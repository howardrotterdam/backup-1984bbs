													<h2>传，胡喜欢听《我的未来不是梦》</h2>
						
						
																											<div id="postmessage_360442" class="t_msgfont">正高　投稿　2009年12月06日&nbsp; &nbsp;from 阿波罗新闻网&nbsp;&nbsp;<br />
<br />
2005年冬天，我被调入中央警卫局担当内卫。俗话说就是中南海看大门的。在那里的日子，我们没有自由，军人们都很敏感，什么也不说，因为里面涉及很多机密，在这里我就不指名，我已经看透了他们腐朽的生活，接下来，我将冒着生命危险向大家透露中南海里面的糜烂生活，糜烂世界。<br />
<br />
我第一天到北京的时候住的是北京中央警卫局三座门招待处，来了一位处长（大校军衔），和我们说保密条例，并且对照简历，家庭情况表，一一问我们问题，非常的严格，说了很多所谓非常重要的注意事项，例如不许和中央领导家女儿，孙女，保姆接触的事项，还有不该问的不问，不该说的不说。只认通行证，不认车的规矩。我当时抱着神圣的使命，认真的听着，但接下来3年我的所见所闻，都深深的出卖的我最初的灵魂思想。<br />
<br />
记得那是05年12月的某一天，我拿上解放军最好的步枪，95步枪里面有30发实弹站岗。这是我第一次用实弹站岗，所以很紧张。我深刻的记着处长和我们说的：每辆车上都有芯片，对着大门上的读卡器，首长车来都敬礼，让我们背车牌号，例如，胡的车是京V02288.京V02289 02266。三辆车，两辆奥迪A8.3.7TT。防弹车，一辆奥迪A6.2.7TT。都是最顶级的配置，后来我认识胡的警卫秘书王福庆，我当时很有幸的坐上了主席的车，音响据说是后装了，花了10万美金，非常棒！听着主席最爱听的命运交响曲，还有《我的未来不是梦》这些歌曲，感觉自己就是主席，我当时心想一句老话，将相王侯有种乎！我在车的后座后面发现一个菠萝，我问王领导为什么放个菠萝，他说，这是最自然的香水。我听了之后有很大的佩服，这个主意很好。。。。。每次主席出去在没有大事的情况下，都是3辆丰田4700越野车开道，后面也是一辆A8，都是京V02的牌子，车速很快，单向封路。车是防反器材狙击枪的玻璃，装甲，防止炸药炸底盘，非常结实！轮胎都是实心的飞机用的轮胎，里面有钢丝。温的牌号是京V02188.02128两辆A8，也是差不多的。<br />
<br />
在中警当了几年兵，见识的还真不少，曾经有一次主席去外国访问，我与不少士兵巡逻的时候被首长让去主席家中干活，我太高兴了！当时就是一种热情，被共匪赤化的热情！于是我进了主席的家中-中南海6号楼。这个楼在外面看着像是一个很普通的小别墅，3层楼，很普通。进去之后我被里面的奢华吓坏了，地毯厚度15厘米，踩上去非常舒服，舒服的不得了，感觉像踩在人肉上的感觉，一进客厅是几个沙发，沙发不是真皮的，都是深绿色的方正的布沙发，但坐上去很舒服，不累。第一个房间有一个警卫室，在客厅的西面，警卫室我没进去，里面有什么我也不知道。客厅的电视是大约有60寸的大夜晶，是日本大金牌的，非常的奢华，那时候06年那么大的液晶我见都没见过。灯是大水晶灯，有种罗马大教堂的感觉。走向2楼，是主席的卧室，有一点我到现在没搞明白，主席的卧室是两张大床，坐上去很舒服，是夫妻双睡，两张大双人床并排摆着。灯光是金黄色的，照在大床上很有品位。床头向着南面，床前依然有个大液晶电视。一共5个洗手间，主席只在2楼上厕所，我进去之后看见水龙头都是镀金的，马桶，浴缸都是水晶的，透明的，我心想，主席上厕所一定能看见没穿衣服因为是透明的。瓷砖都是蒙那里沙。。。。书房有一副大字，是某书法家写的《赤壁》。有一个大地球仪，一张大理石桌子，还有3部电话，红色的，白色的，兰色的。红色保密机是给各常委，各省委书记打电话用的，白色是给各领导家，亲戚家打电话用的。蓝色的是一拿起来，警卫员就进来。。。<br />
<br />
每个首长家有3名保健医，和厨师一起配合保证领导吃的好，吃的健康。他们吃的东西都是1008专供。大米是天津小站大米，特别的一块田地里种植的，含有硒，茶叶也是杭州的龙井，上好的龙井，还有大红袍。据说喝完了还壮阳。他们吃的熊掌，天鹅，鲍鱼，都是最好的野生的。我认识万里的厨师，曾经还给我偷出一个熊掌，我给他拿1000块钱，回家之后找厨师做了，红烧熊掌，东北黑熊的熊掌，肉很肥，很稠，非常好吃！！他们喝的牛奶都是人奶，据说是一些女的刚生完孩子把奶挤到杯子里，他们就喝。中南海还养了好几十只鹿，他们还经常喝鹿茸血。。。。。。我看了才知道，为什么这么多人想当共产党的官，太有诱惑性了、我在那的第二年，中央警卫局采购了20辆保时杰911TURBO。是专门为中央领导子女买的车。<br />
<br />
以上是我第一阶段的发帖，绝对属实！作为一名曾经在所谓神圣的中南海当兵的武警，我感到无比惭愧，中国还有多少老百姓还生活在水深火热，还吃不饱饭，他们这么奢侈，我相信，总有一天他们会后悔，会成为人民的罪人！人民不会放过他们的，他们最终也逃离不了人民的审判！<br />
