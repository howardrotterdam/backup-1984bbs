													<h2>鲜为人知的陕西同治大屠杀</h2>
						
						
																											<div id="postmessage_818663" class="t_msgfont">鲜为人知的陕西同治大屠杀<br />
作者：杜君立 2010-4-16 14:39:15 发表于：博客中国<br />
（提示：本文8000余字。）<br />
<br />
在西方炮火和南方农民战争的双重折磨中，无远见、无胆识、无才能、无作为的“四无”皇帝咸丰在他31岁时死去。中国从此成为一个女人的中国，慈禧以她从戏台上学来的阴谋和智慧，竟然统治中国长达近半个世纪，甚至创造了一个号称“封建社会的最后一次回光返照”的“同光中兴”。<br />
<br />
“中学为体，西学为用”的同光中兴说白了，就是在政治体制不变的前提下进行的经济体制改革。这种中国特色的发展模式如同一个身体巨大的婴儿，貌似成人，实则心智未开。被既得利益集团控制的这场畸形改革，在一场牛逼哄哄的海战后彻底丢掉了“皇帝新装”，吁请改革的六君子血洒菜市口，流氓的专制和专制的流氓带来了中国彻底的崩溃，北京沦陷，伟大领袖慈禧如丧家之犬，逃亡陕西关中……帝国无可挽回地走向它的末日。<br />
<br />
-<br />
<br />
同治之前，席卷整个南方的、长达12年的太平天国战争使中国人口从4.3亿锐减到2.3亿人，几乎造成全国一半人口死亡，而其中直接死于战争只有4000万人。苏、浙、皖、赣、闽五省死亡达7000万，这些中国当时最富庶地区的经济受到毁灭性的打击。<br />
<br />
“中国特色”的太平天国不仅是中华民族历史上一次无比惨重的灾难，也是人类历史上伤亡最大的战争，远远超过第一次世界大战和第二次世界大战。这场浩劫之后，当中国其它地方开始休养生息的同光中兴时，地处西北的陕西又点燃了另一场浩劫的导火索。从明末农民战争之后，长达200多年的关中稼穑田园一朝之间就沦为人间地狱。<br />
<br />
这场席卷陕西，影响整个中国西北乃至中亚政治格局的“同治大屠杀”使富庶的陕西遭遇到前所未有的人道灾难。陕西人口从同治元年（公元1862）的1394万锐减到光绪五年（1879）的772万。短短十余年间，全省人口损失高达622万，有一半左右人口死亡。与太平天国之祸不同的是，同治大屠杀的死难者中绝大多数直接死于战争。<br />
<br />
-<br />
<br />
1<br />
<br />
陕西号称三秦，全省习惯上分为关中、陕南和陕北3个不同的地理板块。以泾、渭为中心的关中盆地传统文化积淀最为深厚，经济最为发达，人口分布也最为稠密。面积仅占全省30%的关中（西安、同州、乾州、州、凤翔等三府两州），人口就占了全省的56%；而占全省面积44%的陕北陕南（延安、榆林、商州、~州、绥德三府两州），人口只占全省的21%。<br />
<br />
作为中国传统文化和农业文明的发祥地，关中历经周秦汉唐，始终为中国的政治经济文化中心。作为丝绸之路的出处，关中不仅是汉文化的标本，而且是中国穆斯林文化的重要历史舞台。相比陕北陕南，关中穆斯林传统历史久远，其先民可以远溯至汉唐时期来自中亚及西亚等地的阿拉伯人和波斯人。近代的西北诸省是我国回族人口分布最集中的地区，而同治元年(1862年)以前，陕西省又是西北诸省中回族人口分布最集中的省份之一。《秦陇回务纪略》卷一中有“陕则民七回三，甘则民三回七”的说法。<br />
<br />
根据“民七回三”推算，1862年以前陕西的回族人口大约有150万到200万。乾隆四十六年（1781年）署理陕西巡抚毕沅在奏折中谈到陕西回民情形时道：“查陕西各属地方,回回居住较他省为多，而西安府城及本属之长安、渭南、临潼、高陵、咸阳及同州府属之大荔、华州，汉中府属之南郑等州县回民不下数千家，俱在巡抚衙门前后左右居住，城内礼拜寺共有7座，其最大者系唐时建立。西安回民大半耕种、畜牧暨贸易经营，颇多家道殷实，较其他处回民稍为体面”。<br />
<br />
以凤翔府下辖的岐山县为例，当时有回民28坊，共6.3万人，平均每坊回民0.225万，1861年岐山县共有20.8万人左右 ，回民比例约为30.3%。回民聚居以清真寺为中心，“凡有回民之处所亦各有礼拜寺”。而一个以清真寺为中心的回民聚居区就叫做“一坊”，整个凤翔府共有回民36坊。1861年，陕西全省共有7府5州90个州、县、厅，总人口1382万，保守估计，陕西回族人口的峰值数不会低于170万，约占全省人口总数的12.2%，他们大多集中在富庶繁华的关中一带。也就是说，关中地区的回民比例要远远高于北边的黄土高原和南部的陕南山区，“民七回三”的说法应当是事实，甚至一些县区回汉人口在伯仲之间。<br />
<br />
-<br />
<br />
2<br />
<br />
关中地区的西安、同州、乾州等府州既是全省回、汉人口最为集中的地区，也是回、汉矛盾最为激化的地区。“回汉杂居，俗尚各别，睚眦小忿，本人情所不能无。”“龃龉相仇杀，视为固然者久矣。”同治初年，随着太平军残部入陕，矛盾随之激化，战争爆发了。<br />
<br />
从1862年初到1869年底，这场战争整整持续了7年之久，整个陕西省，尤其是人口稠密、富甲一方的关中地区成为双方厮杀的主战场。其间除了清军与回军之间的镇压与反镇压外，更有回汉之间的民族大仇杀，大批回民与汉人因此而丧生。<br />
<br />
据杨毓秀的《平回志》记载，同治元年（1862年）4月，渭南县回民与汉民之间因纠纷发生械斗，汉民称“回回造反”，回民说“汉人灭回”，一场残酷剧烈的民族冲突迅速蔓延。随着事态失控，周边大荔及华州等处回民群起响应。关中各县，有的是回民先起来反抗汉民，有的是汉民团练起来洗劫回民。如渭北的耀州、富平、高陵、同官，是汉民先向回民开刀；而临潼、渭南、同州、大荔、华州、华阴一带，则是回民占了上风。三原、富平、华县、兴平等地汉人均遭到残酷屠杀，死亡总数约有100万余人。陕西西安、凤翔二府,及其它州县的回民被成县成乡成村的遭到屠杀。有些县城内，所有回民无一幸免。同样，回军所到之处也是烧杀抢掠生灵涂炭。整个八百里秦川淹没在腥风血雨之中……<br />
<br />
据孔易昭的《平定关陇纪略》记载，从1862年初太平军入陕至1867年底捻军自壶口渡河，这场战争前后共持续了将近6年。陕西全省无一处不受到战争的摧残，由于回汉之间的民族仇杀贯穿战争的始终，因此，战争中的人口死亡尤为惊人。回族巨堡羌白镇被攻破时“堡中老弱妇女哭声震天，尽屠杀无遗。”“长安县属之六村堡，著名富足，居民万余，避难之民附之，又添数千余口，墙厚壕深，可资守御。……被贼攻破，堡中尽被屠戮殆尽。”<br />
<br />
因为穆斯林民兵不杀基督徒，当时陕西甚至一度出现大批汉人改信基督教的情况。<br />
<br />
- <br />
<br />
3<br />
<br />
号称“回民十八大营”陕西回民军攻占渭南县城，围攻同州府城，接着又连陷高陵、华州、华阴，包围省城西安。从1862年10月，西安已被围困了15个月，“粮米穷蹙”。慈禧急调胜保从京师出3万精锐驰援陕西。胜保见回军势大不敢出战，被慈禧赐死。多隆阿接替后，身先士卒苦战一年，终于解围西安，并逼迫10余万回军撤出关中。至1864年底，凤翔以东至西安纵横数百里范围内，回民村堡尽被荡平。值得一提的是，西安城中的回民因为拒绝参加这场“圣战”而免去杀戮之灾，成为唯一幸存的回民部落。<br />
<br />
多隆阿在与太平军的周至战役中战死后，形势发生逆转。1867年1月，西捻军在西安附近大败清军。沙俄支持建立了新疆阿古柏汗国，“捻回合势”逼迫清政府不得不派出重臣左宗棠率湘军西进平叛。<br />
<br />
左宗棠挥军西进，大败各路回民军，十余万回民军屡战屡败。最为骁勇善战的十八大营元帅之一白彦虎逃至新疆的“阿古柏汗国”。“阿古柏汗国”被左宗棠剿灭后，白彦虎率残部背着锅灶瓢盆，赶着牛羊畜口，于1877年12月翻过天山进入沙俄，总算逃出了清军的追剿。<br />
<br />
这些半难民半民兵的“陕西回回”在今天哈萨克斯坦阿拉木图西边约200公里的楚河岸边扎下“营盘”，播种从陕西老家带来的麦种和菜籽，在此繁衍生息。他们自称“东干人”。“东干”为陕西方言“东岸”，意思是东方。100多年后的今天，东干人还固守着家乡的传统，说陕西方言、沿袭晚清陕西的风俗习惯，甚至还唱秦腔。这些“陕西村”里的老汉们对陕西感情更深，遇见有从西安来访古的记者，都能说出一些关中地名，有的甚至问这些从“清国”来的人：左宗棠的人还在不？来的时候，陕西衙门批准不？<br />
<br />
与成吉思汗类似，白彦虎被东干族敬奉为“东干人之父”，却被很多汉人斥为“屠夫”。据说，1864年10月，白彦虎在屠杀了合水的汉人之后，曾大举进攻陕北，企图捣毁黄帝陵，但遭到当地反清武装董福祥部的顽强抵抗。1869年白彦虎再度奔袭黄帝陵，被北路清军击退。<br />
<br />
1871年左宗棠进军甘肃，当地回军首领马占鳌投降，被左宗棠编入清军。这就是后来的西北五马的由来。马占鳌的后代有马步青、马步芳、马鸿逵、马安国等。这个盘踞西北长达70多年马家军曾经成功阻击全歼了2万多西路军，直到1949年在西北失势。<br />
<br />
-<br />
<br />
4<br />
<br />
据说在户县有这样的一个故事，1867年冬天，回军围困了该村牛东乡马家村，从西往东开始挨家杀，杀完人开始放火烧房子，等杀了村子中间，一看有一家店铺，上面写着“马家当铺”，他们以为是错杀了同族兄弟，这才放过了村东人家离去，使得该村半数人家侥幸逃生。当年户县东部临近沣河岸边的地区和长安、兴平、泾阳、三原、渭南等地一样，是汉回大仇杀的重灾区，汉族的人口损失近半，这个回回聚集区在叛乱后已经完全失去了回回踪迹。 <br />
<br />
损失最惨的是临潼县，人基本被杀完。临潼县志载：“1862至1869七年，临潼县死亡人口30余万。渭河南北烧杀之灾无一村一人而幸免。”令人惊讶的是，《中国人口史》一书中列出了很多县的死亡情况，唯独对死亡最惨的临潼县只字未提。<br />
<br />
终南山下的周至县民风强悍，当时各村结寨自保，回回在这里杀人最为残忍，汉民的复仇也最为残酷。有个村子叫萧家村，回回围困该村后全村奋起抵抗，最后回回采用了种族灭绝的方式，一把火将该村男女老少全部烧死，大火两日不灭。还有个叫“五屯”的村庄，主要是回民，在左宗棠的汉军抵达陕西后，人们尊称“左爷来了”，“左爷”的大军对五屯同样进行了种族灭绝，这个村庄彻底从地球上被抹掉了。据当地的老人说，“左爷”来了以后，汉民纷纷带领官军四处搜查回回，周至所有的回回村庄荡然无存。<br />
<br />
这场两败俱伤的仇杀所引发的社会危机成为一道永远也无法抹平的伤痕，深深地刻在关中人的内心。以我在关中的成长经历来看，西安以外，秦人对回民的杀戮仇恨至今犹耿耿于怀。在大屠杀之后，陕西穆斯林仅存西安一脉，这支古老的穆斯林以谦卑厚道、虔诚礼让而迥异于其他地方的同教，也为当地汉人所尊敬。<br />
<br />
-<br />
<br />
5<br />
<br />
左宗棠是这场战争的平定者，也是战后回民安置、灾民救济等一系列善后事宜的处理者，他在给慈禧的奏折中称：“以陕回人数计之，从前无事时散处各州县，地方丁口奚啻数十万，现计除西安城中土著两三万外，余则尽族而行，陕西别无花门遗种。即合金积、河、狄、西宁、凉州等处现剩陕回计之，丁口亦不过数万，其死于兵戈、疾疫、饥饿者盖十之九，实回族千百年未有之浩劫”。陕西巡抚刘蓉亦言“西安同州凤翔三府，地最饶沃，古称陆海，今土地之开垦者十不二三，而人民之死亡者十居六七……向日绣壤相错之地，树木丛生，丫杈成拱，或行数十百里不见一椽一屋一瓦之覆。炊烟昼绝，豺獾夜嗥，气象殆非人境。”<br />
<br />
同治年间的这场浩劫，短短的7年内，2000多万人死于种族大屠杀，相当于30多次南京大屠杀，相当于20次卢旺达种族大屠杀，也相当于100年后的大跃进运动中“非正常死亡”人口的一半。陕西全省人口死亡高达622万。虽然汉人的绝对死亡数量（467万）远远高于回民，但因为汉民人口基数大，回民人口的损失比例远远高于汉人。估计有155万回民在这场仇杀中死亡，占回民总人口的91%，几乎到了亡族灭种的程度。大多数战前回民聚居的州县，战后回民踪迹全无。<br />
<br />
事实上，甘肃的死难人数远远大于陕西。据《中国人口史》统计，咸丰十一年（1861年），甘肃（包括今宁夏和青海部分地区）人口为1945.9万人，战后的光绪六年（1880年）人口仅存495.5万人，人口损失达1455.5万人，比例达74.5％。<br />
<br />
关于这场我的祖辈历经的悲剧，历史总是体现出不同的解读方式。西方史学家称之为“东干战争”，认为这是伊斯兰教徒对异教徒的一场“圣战”。清政府称之为“回乱”，中华民国政府称之为“同治回乱”。虽然中共官方站在阶级论的角度，称这场回汉两个民族的双重悲剧为“反清大起义”或“回民起义”，但在很多历史教科书中，这段悲惨的往事和50年前大饥荒一样，被官方刻意尘封和隐藏。很少有人知道，渭水怎样被秦人的鲜血染红。或许这是一个谁都不愿触碰的伤疤，回避是最无奈又最现实的姿态――陕西人常常解嘲：“他大舅他二舅都是他舅，高桌子地板凳都是木头”。<br />
<br />
-<br />
<br />
6<br />
<br />
发生在同治年间的这场灾难，与其说这是一场战争，不如说是祸乱和屠杀，一场回汉两个民族之间的种族大屠杀。战乱开始时，回民对汉族进行了种族灭绝式的屠杀，后来回民又遭到政府军队种族灭绝式的屠杀。这场灾难对西北地区造成了巨大的破坏。回汉人口损失2000多万。陕西近一半人口死亡，甘肃死亡率达70%以上。汉民几乎在甘肃、宁夏和青海的许多地方绝迹的同时，回民也几乎在陕西消失。<br />
<br />
直到1953第1次全国人口普查时，陕西回民人口总数也不过5.5万人，这其中还包括了不少在二战和内战期间从东部各省陆续迁移而来的回民。1990年全国第4次人口普查时，陕西回民人口总共只有13.2万，仅占全省人口总数的0.4%，全国排名第15位。陕西的回民人口之稀少，这不仅在风俗接近、语言相同的西北5省，而且在北方诸省中这也是令人吃惊的。因为100多年前这场不幸的浩劫，除了西安城内的3万多回民，省内其他地方回民基本绝迹。作为汉文化“子宫”的古老陕西，在今天几乎成为中国最纯粹的单一汉民族省份。<br />
<br />
-<br />
<br />
这场浩劫也是一场财富洗牌，因为人口锐减，富绅大户几乎绝迹，关中成为尼基系数极低（0.2）的自耕农社会，自给自足的小农经济成为主流。当年渭河两岸的清真寺鳞次栉比，如今从西宝高速走过，只有基督教堂上高高的十字架寥寥可数。<br />
<br />
陕西回民如今硕果仅存西安一脉。建于隋朝的化觉巷大清真寺可以说是中国最古老的清真寺，至今依然保持完好。包括这座清真寺在内的“七寺十三坊”是西安回民的聚居区，这些历史古老的穆斯林人被西安人称为“坊上人”，他们制作的传统食品用料考究、工艺精细，品种众多，堪为中国清真饮食之冠。回坊风情街如同欧美城市的唐人街一样，如今是外地人来西安旅游的必去之地，羊肉泡馍也已经成为西安最具诱惑力的一张名片。<br />
<br />
即使回回已经在陕西乡村消失了100余年，但伊斯兰文化对陕西风俗影响的痕迹仍然十分明显。陕西人――特别是关中一带普遍没有酗酒饕餮的恶习，绝大多数人不饮酒不追求美味，这在追求口腹之欲、吃喝风盛行的中国是非常罕见的。同时，关中人都衣着俭朴洁净，反对艳丽铺张，居家讲究整洁有序，头巾和帽子几乎是关中女人的基本装备……在1000多年的回汉长期杂居过程中，具有强大宗教力量的伊斯兰文化对秦人生活习惯耳濡目染的渗透是不可避免的。<br />
<br />
当然这些地域文化也与其较低的尼基系数（贫富差距小）有关，普遍的贫穷要求人们只能省吃俭用勤俭持家，甚至炫耀和攀比都失去意义。整洁是秦人的优点，人们没有因为贫穷而丧失尊严。<br />
<br />
-<br />
<br />
7<br />
<br />
1950年代，中国著名民族史学家、西北大学教授马长寿先生以西北民族研究室的名义对陕西同治大屠杀进行了详细地调查，最后将这些分县区的调查记录整理成《同治年间陕西回民起义历史调查记录》，收录在《马长寿民族史研究著作选》一书中，2009年由上海人民出版社正式出版。除过《中国人口史》对同治大屠杀有所提及外，就当代官方允许的公开出版物来说，这或许是国内目前唯一一部较有深度地正面触及这段黑暗历史的文本记录。<br />
<br />
痛定思痛，100多年来，关于这场巨大的中国悲剧，人们一直都在反思其原因。归纳一下不外乎有以下几点：<br />
<br />
第一是人地矛盾。当时陕西和甘肃的人地矛盾是极为突出的。陕西关中地区“丰年恰足自给”，而甘肃临夏地区“山林砍伐净尽以栽禾谷”。虽然小农经济勉强可以维持温饱，但想要进一步富裕却根本不可能；若是灾年，甚至会卖儿卖女苦不堪言。这就是人们常说的马尔萨斯陷阱：在生活资料产出有限的前提下，只能通过贫穷和罪恶来抑制人口增长。<br />
<br />
当时陕西人口为1400万，相当于现在的1/3。在没有化肥、农药和现代灌溉技术种籽技术的情况下，这样的人口负载已经达到崩溃的极限。即使关中自古尼基系数较低（0.2），但普遍的贫穷和贫穷所产生的恐惧也足以使社会矛盾积累。战争作为一种罪恶和对罪恶的审判，爆发是必然的。在缺少贫富矛盾的情况下，种族区隔成为一种最现成的战争借口。大屠杀之后，人地矛盾得到纾解。<br />
<br />
第二是农商矛盾。回民不崇尚读书，以经商开饭店致富为荣。回族商人中难免出现奸商。回民“没有文化”但相对比较富裕，以“耕读传家”的汉人对此愤懑不平：“回民的饭吃得，话听不得。”<br />
<br />
第三是宗教信仰矛盾。这个原因应是一个主要原因，不同民族之间的生活方式、文化传统最有可能产生芥蒂和隔阂，误会和歧视随时都有可能升级为仇视和敌意。同时，宗教本身的派系和利益也是制造矛盾的引线。据说当时回民军打出来的旗号就是“杀光卡菲尔”。<br />
<br />
第四是整个清帝国的大形势。那是一个中央控制力崩溃的末日时代，因为南方的战事和外交赔款，即使富庶的关中，其民生之江河日下也是可以想见的。正是在这个绝望和焦虑四处弥漫的不幸时刻，捻军和太平天国的到来成为这场灾难的引信。<br />
<br />
-<br />
<br />
8<br />
<br />
在知青作家群落中，“红卫兵”3字的发明人张承志显得极其另类，他不同于人道主义的王小波，也不同于充满反思和批判精神的北岛，更不同于获得诺贝尔文学奖的高行健。无论早年对布尔什维克还是晚年对伊斯兰教义，张承志永远都充满强烈的宗教饥渴。作为红色教育下长大的回民后代，他在《心灵史》中记录了一首流传在靖远的儿歌：<br />
<br />
-<br />
<br />
同治五年三月间，杀气弥漫天。<br />
<br />
十余万人一朝尽，问谁不心酸。<br />
<br />
桃含愁兮柳带烟，万里黄流寒。<br />
<br />
阖邑子弟泪潸潸，染成红杜鹃。<br />
<br />
清歌一曲信史传，千秋寿名山。<br />
<br />
碧血洒地白骨撑天，哭声达乌兰。<br />
<br />
-<br />
<br />
毕业于北大历史系、自称“不属于中华民族”的张承志不仅偏执地认为人们无法理解“红卫兵”，也认为人们无法理解回回：“与我们通常认为的大汉族主义压迫少数民族这一认识针锋相对，靖远汉族知识分子认为，是回民的民族主义和国家对回族的优厚政策，导致了回乱时期苦难深重的靖远汉族知识分子受挫。这是极其罕见的错误认识。我为这种认识感到震惊的原因，并非在我对它的不义的反感，而在我清晰地触碰到的这种──人的隔阂。靖远县是否发生过同治五年三月回民屠杀十余万汉民的惨案，我不知道。但是我相信回民一定有过对汉民的仇杀。人对人是残酷的。乱世从来释放残忍。民族仇杀是历史的一种真实。同治回民起义中，屠杀汉族无辜的现象在陕西回民军中尤为严重──报应是后来陕西籍政府要员对回族的成见。继承刽子手湘军遗风的一些湖南人，以及保持对回乱惧恨的一些陕西人，将是这个世界上最难理解回回民族的人。”<br />
<br />
无论如何，任何歌颂屠杀、赞美屠杀或者否认屠杀、掩盖屠杀的行为，都是没有人性的，也是最为可耻的。对历史的篡改和矫饰也是许多“爱国愤青”最不能原谅当代日本人的原因之一，但“仇日”本身就跟仇富、仇官一样，是经不起任何基于理性的细节推敲的。<br />
<br />
-<br />
<br />
9<br />
<br />
1998年诺贝尔经济学奖得主、哈佛大学教授阿马蒂亚・森在他童年时代，不幸遭遇了1940年代的印度教徒与穆斯林之间的仇杀。这场引发印巴仇恨的绝大多数受害者一一包括印度教徒与穆斯林一一都是同属一个阶级的劳动人民。阿马蒂亚・森认为，应当对这场血腥屠杀负责的是那种以宗教身份进行单一划分的愚昧观念。单一划分要比多元和多种划分更偏于对抗，而后者构成了我们所实际生活于其中的世界。<br />
