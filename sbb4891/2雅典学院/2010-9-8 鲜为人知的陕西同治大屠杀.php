阿马蒂亚・森在《身份与暴力》一书中指出，世界也许充斥着越来越多的暴力，驱使着这些暴行的，不仅仅是那些不可解脱的仇恨，还有人们的思想混乱。与过去一样，一直延续到今天的冲突与暴力都受这种单一身份的幻象影响。显然，越来越多的人们根据宗教立场（或者“文化”或“文明”）来划分世界，而忽略人们看待自身的其他方式，诸如阶级、性别、职业、语言、文学、科学、音乐、道德或政治立场，并且否定了合理选择的现实可能性。一旦根据这种观点来界定不同人之间的良好关系，人类就被严重地压缩并置于“小盒”之中。<br />
<br />
-<br />
<br />
近代以来，民族主义和国家主义相继崛起，沆瀣一气同流合污，这两个主义为人类社会和地球公民铺设了一条最快捷的“通往奴役之路”。从毛万岁的红色恐怖到今日的塔利班，乃至去年造成近千人死伤的新疆事件，无论是国际恐怖主义事件还是中国特色的“群体性事件”，无一不是来自于一种没有选择的身份认同，仇恨和暴力常常压倒人们正常的人道同情与恻隐之心，非理性是产生这种罪恶的重要源头。<br />
<br />
在超越狭隘的宗教界定的前提下，我们必须理性地承认人类社会关系的多样性，并且作为这个世界的共同居民来进行理性地思维，而不是硬把人们塞入一个个狭窄的“盒子”中，也许只有这样，才有可能救赎最容易被仇恨教唆的人类。或者说，不管是在一国之内还是在世界范围内，人们不仅有自由把握自己生活的权利，而且还应当有理性地发表意见的权利。</div>

							
							
							
							
