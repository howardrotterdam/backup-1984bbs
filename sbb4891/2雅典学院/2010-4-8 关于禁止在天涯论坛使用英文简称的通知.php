													<h2>关于禁止在天涯论坛使用英文简称的通知</h2>
						
						
																											<div id="postmessage_553807" class="t_msgfont">天涯社区语言工作委员会<br />
　　<br />
　　天涯社区社会、文学、财经、汽车、IT数码、时尚、生活、情感、娱乐、体育、旅游、图片、视频、活动、体验、世博各频道：<br />
　　<br />
　　天涯社区是天下华人网上的家园。近些年来，随着国家经济社会发展和人民生活水平的提高，广大华人对中华文化的认同感与责任日趋强烈，对规范中文使用的呼声不断加强。968、天涯站务委员会、天涯社区编辑部十分重视广大华人语言使用权益保障工作。在各相关部门的共同努力和推动下，天涯社区语言规范工作取得了积极进展。天涯各频道在版规中对不规范语言进行了具体要求，2010年4月6日，CCTV（中央电视台的英文简称;英语China Central Television，简称CCTV）发布了《避免使用英文简称的通知》，通知中指出“在央视今后的转播中必须要屏蔽一些外文的缩略词，要求主持人口播、记者采访和字幕中，不要使用外语及缩略词。”我们认为这是十分必要的，有意义的，在此基础之上，天涯社区特成立语言工作委员会，经天涯社区讨论通过发布《关于禁止在天涯论坛使用英文简称的通知》，严禁在天涯社区使用任何英文简称，如遇特殊情况必须使用，要在外语及外语缩略词后加中文解释。本规定于2010年4月8日起实施。这对于保障天下华人权益、弘扬尊重华人价值权利的理念具有十分重要的意义。为切实做好华人很好使用天涯，保障华人使用天涯的权利，现就有关事项通知如下：<br />
　　<br />
　　　　一、加强领导，建立广大华人使用天涯过程的协调配合工作机制<br />
　　<br />
　　　　广大华人使用天涯是一项社会系统工程，包括登记、注册、发贴、回贴、写博客、发图片、灌水、拍砖、砸分、围观、潜水等诸多方面内容，既涉及天涯站务各部门职责，又需要社会各有关方面的参与。各级监管、技术、站务、编辑、管理员、首席版主、版主、实习版主、特邀版主、助理版主、版务管理委员会等部门，要加强对广大华人使用天涯工作的领导和协调，各司其职，密切配合，互通信息，完善细化相关管理措施，建立长效管理机制，共同做好华人使用天涯相关工作，切实保障华人在天涯的权利实现。<br />
　　<br />
　　　　二、依据规定，进行华人在天涯发贴的语言审查<br />
　　<br />
　　　　为切实做好天下华人使用语言检查工作，各级管理部门要确定天涯语言使用标准检查业务的责任，严格依据天涯社区制定的《关于禁止在天涯论坛使用英文简称的通知》，进行华人使用语言的审查工作。各级语言审查主管部门要会同同级管理员、编辑加强对版主审查语言规范的工作进行指导。<br />
　　<br />
　　　 　三、细化措施，规范华人在天涯使用语言培训<br />
　　<br />
　　　　为使华人在天涯发贴能掌握发贴技能，由天涯语言工作委员会、天涯社区编辑部组织华人使用语言规范培训试点。在试点基础上，天涯语言工会委员会组织制订《华人语言使用规范培训教学大纲》，编写语言规范使用培训教材。鼓励有条件的版块开展语言规范培训。天涯语言工作委员会、天涯社区编辑部将实施组织对各类版主的培训工作。同时，开展语言使用规范培训的学校要为各级管理员、编辑、版主配备笔记本电脑、G3手机（G3手机就是TD手机，G3手机是TD的手机的代称，TD是TD-SCDMA――英文简称，全称为Time Division-Synchronous Code Division Multiple Access ，即时分的同步码分多址技术）、确保随时上网的通讯设备，按照教学大纲组织培训。各频道级站务主管部门要会同编辑部、语言工作委员会加强对语言使用规范培训工作的指导。<br />
　　<br />
　　　 　四、严格把关，做好发贴人贴子检查<br />
　　<br />
　　　　华人发贴需要进行帖子语言规范审查。各级语言规范审查部门要按照《关于禁止在天涯论坛使用英文简称的通知》要求，加强对发贴人发贴的监督管理，一经发现出现“LZ、LS、BS、SB、TX、MD、NND、NB”等但不限于此的英文简称，凡未加中文解释的，一律封杀，屡教不改者，注销。<br />
　　<br />
　　　　六、完善服务，为华人发贴提供便利<br />
　　<br />
　　　　各级语言规范管理部门要积极为华人发贴提供方便。要推动实施《关于禁止在天涯论坛使用英文简称的通知》要求，与有关部门加强发贴的管理。各级版主要掌握发贴人的有关情况，教育、引导发贴人学习通知精神，严格守法发贴，服从版主的指挥和管理，积极配合语言工作委员会做好有关管理工作。<br />
　　<br />
　　　　七、 开展宣传，营造为广大华人在天涯发贴提供便利的良好社会氛围<br />
　　<br />
　　　　各级监管、技术、站务、编辑、管理员、首席版主、版主、实习版主、特邀版主、助理版主、版务管理委员会等部门要加强宣传工作，依据各自职责，多种形式地开展宣传，使广大华人掌握相关法律法规、培训等有关业务程序和注意事项，掌握发贴常识，同时加强对社会大众的宣传，倡导文明使用语言，让一切假洋鬼子2B（俩逼的意思）停止使用英文简称，为发扬中华语言做出巨大的社会贡献，为有朝一日外国人都学习中文并参加中文四六级考试。共同营造为广大华人在天涯发贴提供便利的良好社会氛围。<br />
　　<br />
　　　　各频道对本地从事发贴人语言审查人审查、审核的标准、语言培训机构予以公布，向社会和广大华人告知。对各版块从事语言培训的培训学校于2010年4月10日前报频道级语言主管部门备案。<br />
