																											<div id="postmessage_320534" class="t_msgfont">作者：存中剑 　　2009年11月08日 <br />
&nbsp;&nbsp;<br />
&quot;十一&quot;长假过后，中共官场频频发生地震，短短一个月内，辽宁省人大常委会副主任宋勇、宁夏回族自治区政府副主席李堂堂和贵州政协主席黄瑶三位省部级高官先后中箭落马。在我印象当中，省部级高官如此频繁的落马自&quot;六四&quot;以来还是第一次。现今中共高层的权斗之烈，由此可见一斑。然而和薄熙来在重庆的唱红打黑以及房峰辉在四中全会上力阻习近平升任军委副主席比起来，三位省部级高官的相继落马只是官场的小地震而已。这三次官场小震究竟是前两次大震的余震呢，还是更猛烈的空前大震之前兆？ <br />
<br />
四中全会刚散场，就在习近平未能如期升任中央军委副主席而引起舆论大哗之际，国务院副总理李克强被安排到中共革命圣地井冈山朝觐。据媒体报导，9月24日至26日，李克强在江西井冈山、南昌等地考察，看望了老红军、红军后代和劳动模范代表。过程中还传达了一个&quot;三不&quot;要求：不辜负先烈，不愧对群众，不枉度人生。而国家副主席习近平的活动反而是参观中国科技馆新馆、出席大亚湾核电站续约仪式。 <br />
<br />
中共中央办公厅是不是把李克强和习近平两人的活动安排反了？井冈山朝觐是党务，本该由十七大上所定下的党太子习近平去井冈山摆太子的POSE,说领袖才能说的话，什么时候轮到他李克强了？更何况李克强话中还含沙射影。是谁辜负了先烈？是谁愧对了群众？是谁枉度了人生？除了代表贪官、代表奸商、代表黑社会的江泽民，除了&quot;走资本主义道路的当权派&quot;，我实在想不出还有谁。 <br />
<br />
参观中国科技馆新馆、出席大亚湾核电站续约仪式是政务，本该由国务院副总理、十七大上定为温家宝后任的李克强出场的，反而安排给了国家副主席习近平。之所以出现这种结果，要么是中共中央办公厅把党务和政务搞错了，要么就是把习近平和李克强的地位弄颠倒了，而前者的可能性几乎不存在。执掌中共中央办公厅的是何方高人？胡锦涛的心腹谋士令计划。原来如此，一切都清楚了，居然是红朝版&quot;狸猫换太子&quot;，习近平被大内总管令计划摆了一道，而令总管的背后当然就是胡主席了。 <br />
<br />
通过这出&quot;狸猫换太子&quot;的闹剧，胡锦涛和他的团派向外界发出了一个强烈的信号，那就是党的第五代领导人只能是李克强，轮不到他习近平。这也就意味着胡锦涛的红色司令部已经下定决心，将不惜一切代价，不择一切手段阻止习近平登基。面对如此严峻的形势，习近平在德国以献书为名公开投靠江泽民的黑色司令部也就不足为奇了。 <br />
<br />
极左与极右，两条截然对立的路线；红色与黑色，两个势同水火的司令部。究竟鹿死谁手？一切都将在十八大之前，在余下不到三年的时间内最终解决。六十年来，中国经历了毛泽东与刘少奇的对决、华国锋与四人帮的对决，以及邓小平和赵紫阳的对决。每一次党内的巅峰对决都为这个国家带来了巨大的变化，也给全体国人造成了深远的影响。今天，在习、李太子宝座之争的背后就是传说中的江胡决战，所有的江胡恩怨都将在这次对决中来个彻底了断。正所谓人在江胡，身不由己。时至今日，任何一方都已没有退路可言了。 <br />
<br />
两军狭路相逢，勇者胜！ <br />
<br />
中共的党内斗争有条规律。凡是强调党内民主的，必是人多势众，在选票上占优势的，比如过去的刘少奇、现在的江泽民。凡是强调从严治党的，必是选票不够，需要靠恐吓取胜的，比如过去的毛泽东、现在的胡锦涛。红色司令部似乎每次都票数不够，需要靠枪杆子来指挥党，看来极左路线一向不得人心。 <br />
<br />
习近平是上海帮在十七大上用党内民主的方式拱上&quot;王储&quot;宝座的，这说明黑色司令部在党内人多势众，占有选票上的优势。如果胡锦涛的红色司令部在不久前的四中全会上已经反客为主，占据了选票上的优势，又何必借助房司令的枪杆子来阻止习近平出任军委副主席？这也说明团派迄今为止，在党内还是少数派，在选票上无法与上海帮相抗衡。如果看政治局常委的构成，那差距就更悬殊了。团派就胡锦涛和李克强两人，就算加上盟友温家宝，也只有三个人。而习近平公开站队之后，上海帮在常委中有六个人，优势再明显不过了。 <br />
<br />
如果按照党内民主的原则投票表决，团派必败无疑。可是一旦让习近平成功升任军委副主席，习的太子地位就难以动摇了，这也就意味着上海帮对团派完全形成前后合围的态势，胡锦涛的败局也就定了。要解此燃眉之急，必须把党指挥枪的原则放在一边，活学活用毛泽东思想，用枪指挥党。所以胡锦涛暗中策动&quot;九门提督&quot;房峰辉大闹四中全会，胁迫全体中央委员就范。这实在是团派情急之下的无奈之举，除此之外，胡锦涛没有任何手段可以阻挡习近平上位。 <br />
<br />
可是如此一来，胡主席就把房司令置于非常危险的境地。上海帮功败垂成，必然恼羞成怒，江泽民和曾庆红这些阴险毒辣的小人怎么肯放过房司令？当年杨尚昆是怎么死的？姬鹏飞又是怎么死的？刘华清的女儿和儿媳又是被谁恶整的？大家都心知肚明。而习近平到嘴边的鸭子又飞了，内心的懊恼可想而知。这次胡锦涛只是借助了房峰辉的力量才暂时阻止了习太子高升，并没能将他废黜。而且就凭团派现在这点实力，能不能在三年内扳倒习太子还要打上一个大大的问号。毕竟人家是太子党，况且又是通过党内民主推选的接班人，只要不犯大错，你胡锦涛又能拿他怎样？一旦习近平登基，他会放过房司令吗？会不报当年的一箭之仇吗？就算他本人气量大，旁人也会千方百计劝他报仇立威。况且从习近平送书一事上看，明显是对胡锦涛还以颜色，他也不是大肚能容的人。 <br />
<br />
正如俄国老毛子以外蒙古作为与中国之间的战略缓冲，俄奴江泽民同样以习近平作为与胡锦涛之间的战略缓冲。胡锦涛借助房司令的枪杆子在四中全会上阻击习近平之后，习太子公开投向江泽民，原先的战略缓冲已被彻底打破，江胡间再也没有任何回旋的余地，必有一场死战。从四中全会一战交手的情形看，上海帮在党内占据着明显的数量优势，团派仅仅是依靠&quot;九门提督&quot;房峰辉的突然袭击才得以险胜，房司令也因此被上海帮恨之入骨。如果上海帮最终胜出，房司令落到江、曾的手里恐怕就生不如死了。 <br />
<br />
可是如果胡锦涛最终战胜了上海帮，房司令难道就可以从此高枕无忧了吗？未必。当年雍正夺嫡靠的就是九门提督隆科多和陕甘总督年羹尧的力量。隆科多是雍正的舅舅，年羹尧的妹妹是雍正的妃子，可是这两位在关键时刻出死力助雍正夺取大权的将领后来怎么样了呢？当年的雍正何尝不曾韬光养晦，今日的胡锦涛又何尝不是外宽内忌？ <br />
<br />
由于团派的人数无论在常委还是在政治局都处于劣势，所以胡锦涛无法通过党内的常规斗争达到彻底战胜上海帮的目的。不能彻底战胜上海帮就不能扳倒习近平。一旦三年后&quot;魔戒&quot;落入习近平之手，团派的下场就可想而知了。历史上中共党内的巅峰对决最后无不是依靠军队的力量决出胜负，尤其是在党内处于少数派的一方更是非凭借军队不能取胜，所以团派要想取胜，最后还是要请房司令出马。 <br />
<br />
可是那个时候出手，意义可就大不一样了。败了，固然不免身败名裂；胜了，又怎能免于功高震主？明朝&quot;夺门之变&quot;中的石亨、清朝雍正夺嫡中的隆科多和年羹尧、红朝文革中的林彪、六四事件中的杨尚昆和杨白冰，哪一个不是为主子力挽乾坤？哪一个又能免于兔死狗烹？恐怕房司令为胡锦涛剪除上海帮之日，就是他自己成为胡锦涛要剪除的下一个目标之时。 <br />
<br />
与其说这是中国将领的宿命，不如说是专制政治的必然。军队不实行国家化，军人就没有独立的人格，也没有宪法的保障。君主与将领之间就是主奴关系。将领的功劳再大，地位再高，生死荣辱也只在主子一念之间。尤其是卷入政治权力斗争的，虽然一时权位显赫，其实已经身入虎穴了。不得虎子，便落虎口。尤其在中共红朝，更是险之又险，林彪父子和杨尚昆兄弟就是前车之鉴。 <br />
<br />
江泽民几次三番图谋暗杀胡锦涛，江胡之间已经势不两立，早晚必决生死。如今又加上一个房峰辉，局面就变得更为复杂。从中国的历史规律来看，三方中至少会倒下一方，其实从长远看，最后只能有一个赢家。中国的政治迄今为止都是零和博弈，从未有过双赢。如果胡锦涛和房峰辉之间能够实现双赢，那么真的要恭喜他们了，因为他们创造了在专制政体中政治双赢的奇迹。而这种双赢，在我看来只有在军队国家化的民主政体中才可能实现。 <br />
<br />
当年楚汉相争，刘邦自己率领的主力部队屡战屡败，可是大将韩信所率的偏师却战无不胜。后来项羽派说客去游说韩信，说楚汉相持，胜负取决于你韩信将军，劝他为自己打算，别那么死心眼。可是韩信看刘邦这人很厚道，对自己也很好，就死心塌地为刘邦打天下，给项羽来了个十面埋伏，送他上了路。哪知当初有口皆碑的沛公在大权在握之后就判若两人，借老婆的手也把韩信送上了黄泉路。几千年来这种过河拆桥的事在中国的历史上还少吗？这不是人的问题，而是政治体制的问题。 <br />
<br />
今天的胡锦涛口碑有刘邦那么好吗？和房峰辉的关系有雍正和隆科多那么亲吗？胡、房二人人共患难的时间有毛泽东和林彪那么久吗？以刘邦口碑之好，雍正与隆科多关系之亲，毛泽东与林彪共患难之久，尚且不能免于鸟尽弓藏、兔死狗烹的政治潜规则，房司令到底对胡锦涛有几分信心？对自己的将来又有多少把握？ <br />
<br />
中共邪党篡国六十年来，杀我百姓、毁我文化、辱我先人、割我国土、残害善良、诽谤神佛、奸淫妇女、巧取豪夺，致使贪腐遍地、道德沦丧、黑道横行、天怒人怨，此诚中华民族史无前例之巨痛大劫。自《九评》横空出世以来，退党大潮兴起，今三退人数已超过六千三百万，足见民智已开，人心思变，民主人权，深入人心，此正英雄奋起，豪杰鹰扬之际，拯救国难，万世流芳之时。房将军若能顺天应人，毅然革命，迎立民主政府，清除百年赤祸，必然功盖当世，名垂千古，虽中兴大唐之汾阳王郭子仪，亦不能与此救国将军相比肩。 <br />
<br />
军人的职责是保家卫国，不该是权力斗争的工具，更不该是屠戮人民的帮凶。然而在邪党的政治体制下，多少优秀的军人要么沦为权力斗争的工具，要么变成屠戮人民的帮凶，究竟有谁能肃清百年赤祸，再造中华军魂？ <br />
<br />
如果历史能够再给林彪父子、杨尚昆兄弟一个选择的机会，他们就不会是那样的命运，中国也不会是这样的中国。今天，历史把这个机会放在了房峰辉将军面前。机不可失，时不再来，何去何从，任君自择。 <br />
<br />
2009年11月7日</div>

							
							
							
							
