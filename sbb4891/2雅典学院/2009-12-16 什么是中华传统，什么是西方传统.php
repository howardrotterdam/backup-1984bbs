													<h2>什么是中华传统，什么是西方传统</h2>
						
						
																											<div id="postmessage_376939" class="t_msgfont">如何看待中华传统，是个两难问题。<br />
第一、中华传统确实落后，不落后近代史就没办法解释了。有民族主义者要把责任都推给满蒙，他们是不知道“锁国”是明朝、日本和清朝都有的政策。<br />
第二、信奉西方思想，尽全力毁灭中华传统的人，象洪秀全等，也不是好人。<br />
所以呢，大约还是得用“中庸之道”来说事，不过这个话有些人肯定深恶痛绝，我就不往下说了。我要说的是：什么是中华传统。<br />
简言之：儒释道；再简言之：儒佛；再简言之：儒家；歪言之：阳儒阴法。<br />
姑且不论“阳儒阴法”没有道理，但有一点是肯定的，就算是阳儒阴法，也绝不是纯法（法家）。纯法的封建王朝，基本上只有一个：秦朝。二世而亡，为千秋百世的前车之鉴。（顺便说一句：秦始皇只比汉高祖大5岁，说起来，秦始皇是灭了6国，但是从这个时间关系上看，其实他灭的是7国，连秦国一起）<br />
把法家说成是中华传统，就等于是在历史的阴暗角落随便扫点什么出来，说是中华传统。<br />
什么是西方传统呢？<br />
西方的传统其实有两个部分，希腊-罗马异教时期的传统的和犹太-基督教传统。这两个部分是可能调和的，但也只有当基督教放弃了干涉世俗事务以后，这两个部分才可能调和。任何一神教思想或起源于一神教的思想，只要它试图掌握世俗事务，就是不可接受的。<br />
记住：民主起源于希腊城邦的政治民主，宽容起源于罗马世界的宗教宽容，弥赛亚（大救星）起源于犹太教，共产主义起源于上帝面前人人平等。<br />
