													<h2>同鹏鹏同学的探讨</h2>
						
						
																											<div id="postmessage_672377" class="t_msgfont">鹏鹏来1984有段时间了，之前我只发了一个关于鹏鹏的帖子，那是我在1984发的第一个投票贴。希望把鹏鹏相关的帖子进行合并，主要原因是我时间有限，当时不想阅读任何关于鹏鹏的帖子。也便于以后集中阅读鹏鹏的相关讨论。现在有点时间，少量读了鹏鹏的一些帖子，就此写几句话。<br />
<br />
在我看来，鹏鹏的观点可以总结为：国家形势大好，政治民情落后，稳定第一，社会问题存在，政府上明下恶。鹏鹏如果觉得总结的不对，欢迎指正。<br />
<br />
就这些观点，除了最后一个我持保留意见，其他我还基本同意。国家的形势与30年前相比，当然翻天覆地，政治文明程度落后有目共睹，稳定、渐进的发展方式是所有民族最幸运的一条道路，社会矛盾大量存在且愈演愈烈。只有“上明下恶”，我了解的信息有限，“上”是否&quot;明&quot;存疑。<br />
<br />
这么多的观点相同，那么我的见解又是什么？<br />
<br />
我认为，政府现在的所作所为基本上是错误和盲目的，<br />
<br />
国家的形势大好是“拨乱反正”的自然结果，不值得特别夸耀和洋洋自得，不思进取。正如我之前的一篇帖子提到，现在的政府已经从极权转入威权，从高高在上不是人家烟火的理想主义转入了“发展是硬道理”的现实主义，现实主义的种种弊端已经凸显。如何真诚面对这些问题，而不是在cctv上一味粉饰太平，在周老虎上官官相护，这是政府的重要责任。<br />
<br />
解决政治民情落后的方法是开民智，育理性；而非曲解历史，排斥文明。政治文明最重要的真实、妥协、理性，这需要从历史和其他国家借鉴。中国的民族主义（爱国主义）明显带有种族自豪和优势情结，这种情结不可能被任何其他民族接受和容纳。中国政治从来缺乏对程序正义的推崇，总是在实质正义的旗帜下实现自己的野心。<br />
<br />
稳定的责任完全取决于政府对于社会矛盾处理的手法，是堵还是疏。这点于建嵘“刚性稳定”和“柔性稳定”说的最妙，我不赘言。总之稳定完全取决与政府，与p民无关，中国老百姓从古至今都是最善良、最有忍耐力、最顺从的民族之一，如果不稳定，只能从管理者身上找原因。<br />
<br />
以上的问题方向走对，社会问题就会逐步解决，人们就会逐步理性，政治就会逐步成熟。<br />
<br />
他山之石，前人镜鉴，可以让我们不要在犯南辕北辙的方向错误；千里之行，始于足下，不求毕其功于一役，但求日拱一卒，我相信我们还是有很多事情可以做。<br />
