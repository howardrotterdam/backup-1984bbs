																											<div id="postmessage_321006" class="t_msgfont">第一眼看到陈晓明，就是一个西装革履的形象，感觉非常西化。他当时整天把斯宾格勒挂在嘴上，我起先还没听清楚，以为他说的是法斯宾德。因为福建人说话通常比较含混，比如把方法论说成谎话论。但他说到他当时所在的福建师大那个孙绍振如何如何能说会道，我却是听明白了。当时，孙绍振和北大的谢冕，还有不知哪里的诗人徐敬亚，在朦胧诗的讨论中名声大噪，被称之为“三崛起”。我读过孙绍振的诗论文章，挺有才气的。<br />
　　当然，陈晓明给我的印像更有才气。后来为刊物第一期筹稿，我特意请他寄文章给我。我读了非常喜欢，专门向《上海文学》的周介人作了推荐。可是周介人看了却不以为然，无论我怎么对他说，他就是不肯发表陈晓明的文章。我记得陈晓明文章里谈到小说的深层结构什么的。他还经常用的一个词，便是从斯宾格勒那里拿来的“第三进向”。我后来才用过这个词，在我谈论从伦勃朗到毕加索的绘画时，感觉不错。陈晓明当时显然已经读了不少书，行文之间的遣词造句颇有特色。我当时非常看好他，并且对周介人没发他的文章，至今耿耿于怀。<br />
　　我后来还特意把陈晓明介绍给吴亮。我记得那天带他到吴亮和程德培在作协的办公室里聊天，吴亮把脚一直翘到桌沿上，脚底冲着陈晓明，让我看了十分不快。我后来为此说过吴亮，指责他不该把脚底冲着人家。吴亮笑嘻嘻地道歉说，他不是故意的，叫我转告陈晓明不要在意。<br />
　　记得陈晓明那次到上海来看我，行程很匆忙。他也给我介绍了一个朋友，叫做徐守淙，是个在文化大革命时由于政治原因坐过牢的女人。那女人跟《走向未来书》的一个编委有交往，后来那个编委在90年代莫名其妙地死了。那女人对聚会特别有兴趣。当她听说我要搞一个文学沙龙时，马上响应说，她可以提供聚会场所。我后来真的搞了一个先锋文学沙龙，后来被误传成“西风”沙龙。上海话先锋和西风，是一样的发音。此事以后再细说。<br />
　　好像是到上海来过之后，陈晓明来信告诉我说，他上北京读博士生了，在中国社科院文学研究所。他读博士期间，结识了一些朋友。他告诉我说，他跟靳大成，成燕谷是哥们。然后还有些比较夸张的话。但不管他怎么说，我觉得他是可以自信也是应该自信的。<br />
　　陈晓明可能是九十年代继我在八十年代的一系列长篇大论之后，为数不多的几个对新潮小说继续进行思潮性研究的评论家。而且，他出手的文章也不短。九十年代对新潮小说的评论，我本人看重两个人，一个是胡河清，一个是陈晓明。陈晓明毫不讳言他曾读过我《钟山》上那篇三万字的长文，《论中国当代新潮小说》。我说他毫不讳言，是指他的方式跟我很不相同，他完全可以不提我的文章。我说他毫不讳言，更是指他不像北京和上海的许多同辈同行，对我此文始终讳莫如深。我记得此文发表之后，李陀倒是专门给我写过一信，既大力赞扬又表示不能同意我对马原的评价。但李陀后来也像是忘了一样，从来不对任何人提及此事。李陀也罢，陈晓明也罢，还有其他许多同代评论家也罢，大家其实都知道，我那篇长文，是八十年代唯一的一篇对整个新潮小说在文学思潮层面上展开的历史性论说。那篇文章中，凝结了我几年的新潮小说评论和研究，本来完全可以写成一部专著，由于当时我突然转入了文化批判，才应《钟山》杂志的稿约，一笔挥就了结掉了。<br />
　　由于后来突然发生的历史事变，对新潮小说的评论和研究一度中断。后来是因为有了胡河清和陈晓明等人的有关文章，八十年代的文学思潮，才得以在九十年代被继续言说。坦率说，相比之下，张颐武似乎更擅长短平快的评论，没有陈晓明那样的纵横捭阖能力。但张颐武毕竟也在新潮小说的评论上，独树一帜。比起那些跟小说对话只能从巴金说到王安忆为止的评论家，张颐武再不济，也比他们强得多。<br />
　　我对陈晓明热衷的后现代理论有所保留，但我认为他在九十年代对新潮小说评论上的实绩，与胡河清并驾齐驱。我奇怪的只是，他后来为什么突然沉寂了。我只是听说，他去了趟欧洲，然后就没有声音了。在这之前，我在上海的一个什么会上与他相遇过，我当时还在会上说了他一通。他被我说得不住地咕哝，说我是个文化杀手什么的。他根本不知道，在当时的会场上，我最看重的恰好就是他。其他那些装模作样的学人，没有一个比得上他的才气。比起我后来对那些人的质疑，我在会上说他的那些话是很轻很轻的。我在会上说了个寓言，叫做《强盗与书生》。我当时并非是专门意指陈晓明，现在想想，陈晓明确实有点像碰上强盗的书生。<br />
　　在我的印象中，陈晓明的理论素养相当不错，而且不喜咬文嚼字，更不乱玩概念游戏。假如他有什么欠缺的话，就是生命底气不足。他说话有时左顾右盼，不敢把话说到底。他跟我说过，他喜欢彻底之类的字眼，但他本人却是个很不彻底的人。在新潮小说的评论上，他没有彻底。在后现代理论的推广和阐释上，他似乎也没有彻底。其实，他是整个中国后现代思潮中最有阐释能力的一个学人。可以说，在对后现代文化理论的理解上，国内至今没有一个人比得上他。可是不知为何，他全都半途而废了。<br />
　　我记得他有一度做过王朔开的一家文化公司的理论顾问，好像叫做总经理吧。那也是九十年代的事情了。他因此到上海来组过稿。记得在他代表公司给上海评论界朋友摆的宴席上，他一口一个朔爷，叫得十分认真。比起当年口口声声的斯宾格勒，这朔爷叫得实在太搞笑了。虽然我对王朔小说和王朔本人，全都印象不坏。<br />
　　我印象中那么西化的一个学子，最后归化到朔爷门下，这究竟是怎么回事？中国的事情，有时候越看越糊涂。我至今没有弄明白，陈晓明究竟怎么了？当年在桂林会议初识时，我还没读过斯宾格勒。可是后来我很想跟陈晓明谈谈斯宾格勒了，他却跑到朔爷那里去了。也许是他觉得王朔有他所没有的生命底气？或者是他觉得王朔的智商比他高？不明白。也许不是我不明白，而是这世界变得快。<br />
&nbsp; &nbsp;&nbsp; &nbsp; -----李拢褐泄八十年代文学备忘</div>

							
							
							
							
