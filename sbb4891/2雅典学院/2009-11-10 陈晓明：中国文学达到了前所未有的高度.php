																											<div id="postmessage_320917" class="t_msgfont">陈晓明：中国文学达到了前所未有的高度<br />
2009年11月09日 07:49羊城晚报<br />
编者按<br />
<br />
针对王蒙不久前提出的“中国文学处于最好的时候”观点，本版在10月24日、31日的版面陆续推出系列评论，引起读者关注。正当争论未息之时，日前在第二届“世界汉学大会”上，北京大学中文系教授陈晓明与清华大学哲学系教授肖鹰、德国汉学家顾彬等人发生争议，又提出了“中国文学达到了前所未有的高度”的说法，在此我们发表陈晓明的特约文章如下。<br />
<br />
同时，我们也继续发表读者来稿，并就相关问题采访了暨南大学党委书记、中文系教授蒋述卓。中国文学到底处于什么样的阶段？对中国文学到底应该作怎样的评价？中国文学存在什么问题？未来走向如何？也请广大读者积极参与发言。<br />
<br />
中国文学达到了前所未有的高度<br />
<br />
很不同意顾彬的评价<br />
<br />
顾彬先生说的语录―――中国文学是垃圾―――尽管顾彬先生申辩说他不是这样的意思，但这样的语录要改变很困难。我们要允许汉学家的误读，同时要保持差异性。我觉得这种态度是我所欣赏的。其实对中国文学的理解，未必全是顾彬先生的误读，同时还有媒体的误导。但我们中国的文学研究者要保持自己的差异这是最重要的，到底是什么样的差异性，就是无作为的差异性吗？<br />
<br />
90年代初张法、张颐武、王一川一起合写了一篇文章试图提出“中华性”的问题，但很快地被国内学界打压下去了。因为人们从中看出一些民族主义的味道。杜维明有一个观点，我觉得很值得重视，他说西方学者都有“知识谱系”，但中国当代学者是没有“知识谱系”的。这句话是很刺激我的。我觉得我从21岁的时候开始读康德黑格尔，一直到今年我还出了一本关于德里达的书，我非常清楚我做这项研究面对的知识谱系是什么，但作为中国的学者，我又确实不知道怎么从老子、孔子再到马克思、福科和德里达来建构一个协调而清晰的知识谱系。因为知识谱系的不清晰，我们的立场和方法也并不明确。<br />
<br />
作为研究中国文学的学者，洪子诚研究了五十年，我研究了快三十年，但面对世界文学的框架，我们没有对中国当代文学的价值的发言权？中国文学60年的历史，我们有没有办法在世界文学的框架中来给它确立一个价值？我们有没有办法去看待和评价它？我们在这一世界性的语境中的立场是非常混乱的。我们没有办法在世界文学的价值体系中解释这60年。<br />
<br />
顾彬先生对中国现代文学的研究我是非常佩服和赞赏的，尽管对你的立场和观点我依然有批评。但你对中国当代文学的评价我是不太同意的，甚至很不同意。</div>

							
							
							
							
