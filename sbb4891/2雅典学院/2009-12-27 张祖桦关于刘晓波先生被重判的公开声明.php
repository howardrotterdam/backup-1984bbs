																											<div id="postmessage_399365" class="t_msgfont">张祖桦关于刘晓波先生被重判的公开声明<br />
 来源：维权网 作者：张祖桦&nbsp; &nbsp; <br />
<br />
&nbsp; &nbsp; 一、2009年12月25日，北京市第一中级人民法院以“煽动颠覆国家政权”的罪名，判处刘晓波有期徒刑十一年，剥夺政治权利二年。对于这一判决，我感到无比愤慨！我在接受媒体采访时明确表示：对刘晓波先生的重判是“在这个精心选择的日子，中国执政当局作出了一个最无耻，最野蛮，最不得人心的判决！”我对这一判决给予严正谴责和强烈抗议！<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 二、世人皆知，刘晓波是因《零八宪章》而获重刑。我作为《零八宪章》的主要起草人和首批签署人之一愿意承担我应负的所有责任。这一点我从2008年12月8日被警方传唤到之后的历次联署签名中已多次明确表达。2009年12月1日，国保警员就《零八宪章》公布临近一周年找我谈话时，我再次申明：我早就做好为《零八宪章》坐牢的准备，我愿意陪我的好友刘晓波一起坐牢；同时，我再次要求立即无条件释放刘晓波。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 三、北京市第一中级人民法院对刘晓波的《刑事判决书》中：本人十分惊诧地看到这段文字：“证人张祖桦的证言证明：他与刘晓波于2008年年底共同制作完成了《零八宪章》，他也征集了签名，后刘晓波将《零八宪章》发表在境外网站”。这根本不是事实。2008年12月8日我被警方传唤时，当主审警员问到其他人包括刘晓波是否参与起草《零八宪章》时，我的回答是：“不知道。你们跟我打了这么年的交道应该清楚我的原则：我只回答我个人做过的事和相关问题，其他人的事不要问我，我也不会回答。”（参见附：张祖桦“传唤、抄家亲历记”，载开放出版社《零八宪章》[珍藏本] ）而且12月8日警方同时抓捕我和晓波时，《零八宪章》尚未公布，我怎么可能指认“刘晓波将《零八宪章》发表在境外网站”？显然，这个说法是毫无根据的。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 注：此声明提交给刘晓波的代理律师作为本人的证言。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 张祖桦<br />
&nbsp; &nbsp; 2009年12月26日<br />
