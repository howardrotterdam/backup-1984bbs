<br />
张祖桦：“传唤、抄家亲历记”<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 2008年12月8日（星期一）晚上23点，我刚从楼下散步回到家中（注1：我12月7日下午刚从德国访问归来）。突然传来一阵急促的敲门声。我妻子隔着房门问“哪位？”回答是“派出所的”。我和妻子对着家门的猫眼往外看，只见室外走道里站满了身着警服的人员。我上前将门锁打开，至少有10几名警员蜂拥而入，其中有两人手持摄像机不断拍摄。我对他（她）们说：请出示你们的证件！为首的一位身材高大的警官拿出了自己的工作证，上面工作单位一栏写着“北京市公安局”，姓名为“姜庆杰”，上面没有标明职务。随行的两名警察分别向我出示了“传唤通知书”和“搜查证”，并要我在上面签字。“传唤通知书”上的罪名栏上写着“涉嫌煽动颠覆国家政权罪”。我镇定地在上面签了我的名字。然后要求他们将这两份文件的副本交给我或我的妻子，遭到对方的拒绝，他们说按照规定要存档，所以不能给我。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 随后，警察催促我马上和他们一起走。我穿上外套，被告之带上身份证和手机。临出门时我跟妻子说了声“放心！没事的。”就随多位警员踏出家门。下楼后，看到楼门口停了一排车辆，看样子来了许多警察。我被请进其中的一辆轿车，前后共有四名警员将我包夹在中间。车开动后，不一会儿到达我家附近的万寿路派出所。下车后，我被带到一幢小楼的楼上会议室中。我一看会议室的对面就是2004年12月 13日我因参与撰写《中国人权报告》被第一次正式传唤的房间，四年后我又“二进宫”了。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 会议室的当中有一张很大的椭圆形会议桌，我被安排在会议桌内侧面对大门的一把椅子上坐下，对面的两排椅子上坐着六名警察，其中那位为首的姜警官宣布询问开始，让一位年轻的警察记录。首先按照审讯的惯例将我的个人履历和家庭基本情况说了一遍。随后问我“知道为什么传唤你吗”？我说：“是你们传唤我，我怎么知道是为什么”？他接着说：你知道《零八宪章》吗？我说：知道。他说是你起草的吗？然后拿出一份《零八宪章》（征求意见稿）给我，我看了一遍后回答说：我确实参与了这个文本的起草，但不是全部（注2：警方出示的“征求意见稿”是一份比较旧的版本，与日后公布的正式本有较大不同）。他问：你都参与了哪些部分？我说：其中关于自由、民主、人权、法治、宪政和联邦制的部分和正文中的一些条文，我参与了起草并提出了修改意见。他再问：都还有谁参与起草？我说不知道。你们跟我打了这么年的交道应该清楚我的原则：我只回答我个人做过的事和相关问题，其他人的事不要问我，我也不会回答。接着他问：你们起草这个宪章的初衷和目的是什么？我说：我不能代表其他签署人，我只能说我的初衷是为了推动中国的人权、民主、法治与宪政建设，推进政治改革，表达公民社会对中国未来发展的建设性意见。这些有什么错吗？违反了哪条法律？为什么要这么大动干戈？对方避而不答。仍然反复纠缠在我和《零八宪章》的关系上。我补充说：我虽然参与了其中部分内容的起草，但是我对全部内容都赞同，并愿意为之负责，即使为之坐牢也在所不辞。对方之后又问了一些起始时间方面的问题，就宣布暂时告一段落。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 中止了一会后，询问者又问了我和维权网的关系，我于今年六月访问美国，不久前访问德国等三个问题，但都比较简略，显然不是这次传唤的重点。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 关于我和维权网的关系，我的回答：我是维权网的义工或者叫志愿者，你们说我是维权网在国内的负责人我也不否认。我们是在依照《国际人权公约》和中国的宪法、法律，进行公民维权，帮助弱势群体，维护公民的合法权益有什么不好吗？他们照样避而不答，也未继续讯问。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 关于我今年6月的美国之行，我的回答是：我以前已和海淀区国保大队的警员说过，纯粹是应朋友的邀请进行的私人访问，住在纽约附近的一个小镇上，并没有参加过什么政治性的活动。对方问：有没有去过国务院？我回答说：我没有接到过美国国务院的邀请，怎么可能去呢？到此为止，对方没有再往下问。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 关于12月的访问德国。因为是应德国外交部的邀请，所有行程都是公开的，一清二楚。讯问者只是一带而过。没有什么可说和可记的。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 大约到凌晨3点，姜警官接到电话出去，长达四小时的正式讯问告一段落。过了大约半小时，姜警官回来让负责记录的一位年轻警员将审讯记录拿给我看，我从头到尾看了一遍，指出有两个地方不够准确，他们商议了一下都按我的意见作了修改，我就在上面签字画押（按指印）。在此期间，一直有两台摄像机对着我拍摄。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 之后，在场的几位警员轮留休息。我也靠在椅子上和姜警官聊天儿，聊到无话可说就闭上眼睛休息，一直持续到第二天（12月9日）上午接近11时。姜警官再次外出回来后很正式地对我说：一会儿你可以回去了，我要严肃地对你提出警告和劝告，你要“到此为止”。我问是什么意思？他说就是不要再主动推动《零八宪章》，不要召开新闻发布会或记者招待会。我说本来就没有这样的计划，何况在你们的严密监控下我连人身自由都没有，怎么可能召开新闻发布会？他说：这个事没完，我们还会再找你。然后问我有什么意见？我先肯定了参加讯问的几位警员自始至终比较文明礼貌，多次主动给我倒水喝，后半夜也没有打扰我休息（当然是在椅子上坐了一夜）。然后话题一转郑重地对他说：“首先，你们今天的行为严重地侵犯了一个公民的人权，我对此提出严正抗议！第二，请你转告这次行动的决策者，不管他是谁？是哪一级机关？身居什么样的高位？他们的这个决策是非常愚蠢的，因为它不可能阻止《零八宪章》的发布，而且等于是给《零八宪章》作了一个巨大的广告，必将遭到国际舆论的强烈谴责，从而严重损害中国政府一直以来苦心经营的国际形象。都什么年代了？还在“以言治罪”。官方（包括警方）和民间有意见分歧，可以通过对话协商来解决，完全没有必要动用国家机器，采取对敌斗争的方式来压制民间的不同声音。你们的这种做法无助于建设“和谐社会”，只能加剧官民对立与冲突，积累仇恨！实在是不明智！”对方回应说以后我们可以继续沟通；我说这正是我的一贯主张。我请姜警官留下联系电话给我，他不肯，说“我会请你喝茶的。”我说“那好！我等你邀约。”<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 9日上午11时许，姜警官和两位警员驾车将我送回到我家楼下，然后礼貌倒别。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 妻子见我到上午11时还未回到家中，以为会延长对我的拘禁，很为我担心！当见到我回到家中，才放下心来！（注3：我在9日早上大约八点左右曾提出给我妻子打个电话，说一句话报个平安。但遭到警方拒绝。他们还试探性地说要带我换一地方去呆几天，被我严词拒绝。）妻子向我出示了警方搜查（抄家）后留下的一份《搜查笔录》（见附件1）和5页《北京市公安局扣押物品、文件清单》（见附件2）。署名是北京市公安局侦查人员：马凯、朱旭；见证人：赵永杰；记录人：崔伟。据妻子讲述：我被带走后，家里留下11位警员，先后进行了两轮大搜查，共抄走我的书籍、资料和通讯录、名片册50余件。被抄走的书籍大部分与1989“六四”有关，其中有几本是天安门母亲丁子霖老师亲笔题字赠送给我的。此外还有笔记本电脑4台（其中1台是我妻子的，1台是朋友放在我这的），光盘47张。人民币现金5万元整，美元现金5千6百元，港币5千3百元，银行卡5张，存折5本。（注4：银行卡和存折大部分是我妻子和岳父岳母存放在我家保险柜里的，现金和外币也是。我妻子向他们作了声明，但抄家的警察说这些属于夫妻共同财产，仍在查抄之列。但他们没有解释为什么连岳父岳母存放在我家的钱财也要抄走）。朋友的护照一本。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 负责抄家的警察先给我妻子留下1000-2000元现金作为我们全家的生活费，经我妻子再三交涉（我们双方有四位老人需要照顾），最后留下1万元现金。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 谢天谢地！抄家过后，我的办公桌的抽屉中还剩下两本物件，一本是我的护照，一本是我不久前刚办的港澳通行证。我不知道这意味着什么？但我知道近期出国或去港澳旅行是不现实的。我当然不会心存幻想！</div>

							
							
							
							
