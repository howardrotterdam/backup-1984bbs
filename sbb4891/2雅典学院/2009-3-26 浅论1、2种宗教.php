													<h2>回楼上几位：</h2>
						
						
																											<div id="postmessage_137816" class="t_msgfont">对待一个目前无解的问题，不同人总会有不同态度；正如学生时期，争论一道难题时，虽然“好学生”的胜率偏高，但总会有些傻x“好学生”在言论中歧视傻x“差学生”，然后大家就开始互相排挤。然而那些喜爱<strong>偷换概念</strong>和<strong>宣扬纳粹</strong>的傻x言论其实很滑稽，持这种观点的人又好像文G时期的小红们那样爱趾高气昂，尽管从我们文化的通病看去，这不是“学生”们的错；但你们说说看，小红们当时为什么就非得去犯傻呢？<br />
<br />
我不打算细回你们的贴，只提几个点你们可以自己去验证看看：<br />
1、“生物进化论是现代生物学的中心原则”――真是这样吗？那这个条目难道写错了：<a href="http://zh.wikipedia.org/wiki/%E7%94%9F%E7%89%A9%E5%AD%A6" target="_blank">http://zh.wikipedia.org/wiki/%E7%94%9F%E7%89%A9%E5%AD%A6</a><br />
