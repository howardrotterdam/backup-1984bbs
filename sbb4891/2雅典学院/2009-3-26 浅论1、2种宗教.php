3、“宗教本源是人的狂热，是非理性的”――关于狂热和非理性，建议参考一下神经科学的观点。<br />
总而言之，在转述观点前不验证，不给出处，是很危险滴～<br />
<br />
以下这段话出自著名的“熊猫审判”，你们可以去看看法官的判决：<br />
“宾夕法尼亚的学术规定要求学生学习达尔文的演化理论，并在最后进行一项标准化测验，而演化理论是其中的一部分。<br />
因为达尔文的理论是个理论，仍然受到新发现的证据所测试。理论并不是事实，此理论中存在漏洞，也就是没有证据的部分。理论的定义是，能够与广大范围的观察一致符合，并受到充分试验的解释。”<br />
<br />
与之相对应的，还有个“猴子审判”。这两出审判都只说明一个结果：对于一个目前无解的问题，胡说八道除了会吃官司外，对得出答案起不到任何帮助。<br />
