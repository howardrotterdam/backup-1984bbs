作家讨回公道，还支持探索，鼓励创新，意识流、荒诞、变形、魔幻<br />
<br />
等西方现代派表现手法体现在不少新作中，用大视野挖掘社会问题和<br />
<br />
政治问题的报告文学成为一种新的创作模式。<br />
<br />
 <br />
<br />
在探讨中国在现代文明道路上的曲折和落后时，不少人为躲避危险绕<br />
