<br />
新闻界不得不放低姿态，佯作收敛。<br />
<br />
 <br />
<br />
鉴于经济形势对理论更新的渴求，理论工作者只得绕开新闻自由这一<br />
<br />
命题发挥影响力。著名经济学家吴敬琏提出，应当创造一个比较宽松<br />
<br />
的环境，以有利于经济改革措施的实施[4][4]。理论界人士纷纷出<br />
<br />
手，从学术开放、学术自由、面向世界、面向未来的角度，冲击僵化<br />
<br />
教条。<br />
