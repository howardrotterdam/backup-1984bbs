<br />
松，有弹性一点[9][9]。形成了朱厚泽倡导的「三宽」政策。<br />
<br />
 <br />
<br />
「三宽」精神给思想文化界倾注了活力，报刊杂志，各路人物齐齐上<br />
<br />
阵，畅所欲言、认为应当将民主扩展到社会生活的方方面面，允许有<br />
<br />
人不用马克思主义研究问题，允许在法律范围内公开地自由地讨论政<br />
<br />
治问题，报纸登什么不登什么的权力应归编委或总编辑，让报纸可读<br />
<br />
可亲可信，不要动不动就无限上纲上线，打棍子扣帽子抓辫子整人。<br />
<br />
一时间，报刊体裁题材五彩缤纷，生动活泼。<br />
<br />
 <br />
<br />
新上任的文化部长王蒙不仅为近年在创作中遭受不公正对待的作品和<br />
