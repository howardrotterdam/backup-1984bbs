<br />
自由上大加发挥。随后，著名记者陆铿专访胡耀邦[3][3]，在谈到海<br />
<br />
内外对这篇讲话一片谴责时，胡耀邦列举了新闻界在浮夸风肆虐的大<br />
<br />
跃进时代所起的推波助澜，甚至火上加油的作用来辩解。显然，这篇<br />
<br />
讲话包含着胡耀邦对出现改革失控的当时，新闻界的不恰当鼓吹可能<br />
<br />
引致揠苗助长恶果的担忧；不过，从胡耀邦一贯的品行为人看，其间<br />
<br />
或许包含更多的是来自「左」边的压力，迫使他必须冒天下之大不韪<br />
<br />
挺身站在扼制新闻自由的第一线。<br />
<br />
