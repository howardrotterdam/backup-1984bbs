<br />
于在7月被思想开放，颇有素养的胡耀邦爱将朱厚泽取代。虽然胡乔<br />
<br />
木、邓力群的职位仍在中宣部长之上，到底不是「现管」。<br />
<br />
 <br />
<br />
默默无闻上任的朱厚泽，坚定又毫不张扬地为新闻和理论界松绑。宣<br />
<br />
传阵地再趋活跃，报刊上公然提出，改革需要宽松的舆论环境，它对<br />
<br />
改革的成功甚至比经济环境更重要[5][5]，用迂回术撞击控制新闻自<br />
