证实，所谓赵紫阳儿子经商，所做的生意还不到一百万人民币，而透<br />
<br />
露出这个消息的正是那些想置赵紫阳于死地的，包括陈云与李鹏的部<br />
<br />
下。当然，传播这些消息的恰恰是赵紫阳想保护的激进学生。当时一<br />
<br />
些强硬派甚至要借学生攻击赵紫阳儿子经商的事，建议先把赵的儿子<br />
<br />
控制起来，以要挟赵紫阳。如果我们稍微有头脑，或者别以为真理在<br />
<br />
手，所向无敌，稍微讲一点策略的话，就应该知道，在那种情况下，<br />
<br />
赵紫阳应该是团结的对象。如果当初真有一个良性的上下互动，情况<br />
<br />
可能会不一样。<br />
<br />
<br />
<br />
博讯记者：可温家宝不是赵紫阳，你把这两个人相比较，不太妥当<br />
<br />
吧？<br />
<br />
云飞扬：你怎么知道温家宝不是赵紫阳？如果没有天安门事件，你又<br />
<br />
知道赵紫阳是什么样的？别神话赵紫阳，他在位前后也近十年，也发<br />
<br />
表了不少讲话，做了好多年总理和总书记，你比较一下就知道。是天<br />
<br />
安门广场把赵紫阳推向了历史的高度。温家宝当今的天安门广场在哪<br />
<br />
里？就在网络上，温家宝利用网络突出理念，你们也看到了，他很多<br />
<br />
话除了网络上可以流传出来外，中共的各大媒体根本不刊发。要说网<br />
<br />
络维权，温家宝就是做的比较好的，作为总理，党控制的报纸不发表<br />
<br />
他的言论，他就不停地利用和大家交流的机会在网络上“发布新<br />
<br />
闻”，希望有所突破。<br />
<br />
博讯记者：但总感觉到还是没有大的突破，而且在近期内也看不到希<br />
<br />
望。和那些批评温家宝的人相比，你是不是太把希望寄托在温家宝身<br />
<br />
上？<br />
<br />
云飞扬：和你说的恰恰相反。我感觉到那些动不动就猛批温家宝的人<br />
<br />
反而是因为把希望寄托在一个人身上而感到失望甚至绝望所致。我们<br />
<br />
说应该和温家宝党内的稍微开明一些的人建立良性互动，不是说把希<br />
<br />
望寄托在他们身上，而是寄托在我们自己身上。别忘记了我前面所说<br />
<br />
的，赵紫阳是天安门的产物，如果没有学生们的天安门，有赵紫阳的<br />
<br />
天安门吗？同样的道理，那些整天期盼温家宝拍案而起的人，你自己<br />
<br />
做了些什么？如果你自己整天在那里连“作秀”都不去做，指望温家<br />
<br />
宝做什么？<br />
<br />
博讯记者：今天正巧，就在采访你之前，看到国内著名作家杨恒均博<br />
<br />
士一篇写戈尔巴乔夫的文章，里面有这样一段话：“戈尔巴乔夫不是<br />
<br />
没有碰上困难，例如每当他言论出格的时候，总有一些人攻击他‘作<br />
<br />
秀’，而每当他提出新的改革措施，又总有人到处散布他妻子赖莎如<br />
<br />
何招摇、家族如何腐败的消息，而且，他在很多公开场合宣扬一些人<br />
<br />
道与自由价值的讲话，到了《真理报》发表时，总会掐头去尾，甚至<br />
<br />
压下全文不发。不过，人心思变，是党内强大的要求变革的力量，以<br />
<br />
及党外更加强大的渴望改变的民意，始终支撑着戈尔巴乔夫的改<br />
<br />
革。”看起来，受到攻击的不只是温家宝，还有戈尔巴乔夫。<br />
<br />
<br />
<br />
云飞扬：这两个人不好一起比，相差还很远。我想强调的是各方面都<br />
<br />
要努力，但要有一定的策略，也要有一定的宽容，特别是那些和我们<br />
<br />
有些共同之处的人，不管他在体制内还是体制外，多一些沟通、理解<br />
<br />
和宽容，事情会好办多了。<br />
<br />
 <br />
<br />
・・・・・・・・・・<br />
<br />
 <br />
<br />
闵良臣：伍皓的素质<br />
<br />
 <br />
<br />
对演讲者不满，包括口头表达、作出不伤害演讲者身体的某些举动，<br />
<br />
以示抗议，这在文明社会虽然也没“成为”伍皓所说的“常态”（其<br />
<br />
实根本就不可能成为“常态”），但也确是家常便饭，常有的事，人<br />
<br />
家根本不以为奇，更不会还把此举与不满者的“素质”联系起来，甚<br />
<br />
至因此就判定不满者素质不高。否则，对不论是在伊拉克小布什演讲<br />
<br />
时向其扔鞋的那位伊拉克记者，还是在英国剑桥大学把鞋子扔向正在<br />
<br />
演讲的温家宝总理讲台的欧洲籍在读研究生，你也能简单地把他们归<br />
<br />
结为素质不高吗？最搞笑的是，那位向美国前任总统小布什扔鞋被判<br />
<br />
入狱、后来被释放的伊拉克记者蒙塔兹・扎伊迪，去年十二月一日在<br />
<br />
巴黎演讲时，也遭受了一回被扔鞋的“待遇”。而这次向他扔鞋的，<br />
<br />
不仅是他的同胞，还是他的“同仁”，是“遭到流放的”伊拉克记者<br />
<br />
扎伊迪，他认为蒙塔兹・扎伊迪站在独裁政权的一边，故在其演讲时<br />
<br />
愤怒地向他也扔了一只鞋去。只是，他并没认为被扔者就是素质不高。<br />
<br />
当然，如果他那样认为，自己也就成“不高”的了。<br />
<br />
 <br />
<br />
若是再听听法学专家所言，就更会明白：在特定情形下的扔，可以<br />
<br />
说，甚至还是一个公民的权利。既然是权利，那么对不满者一扔，也<br />
<br />
就不过是行使一下自己的权利而已；行使权利嘛，有时就顾不得对方<br />
<br />
高兴不高兴、宽容不宽容了。生活中我们不是也常常看到公民行使正<br />
<br />
常权利，有些人不高兴不理解不宽容的例子吗？所以说，如果是正常<br />
<br />
行使权利，对方如何反应，无所谓。《潇湘晨报》记者在伍皓遭扔钱<br />
<br />
事件后采访了湘潭大学法学院教授欧爱民，他告诉记者：我国宪法规<br />
<br />
定公民有言论自由，而言论自由是指公民有通过口头、书面、行为等<br />
<br />
途径表达自己思想、观点的权利。这一点，身为一省宣传部副部长的<br />
<br />
伍皓就未必弄懂了。你看，伍皓在接受潇湘晨报采访时，记者问他：<br />
<br />
 <br />
<br />
“如果今天扔钱的人演讲的时候，给你一个机会，你会不会扔五毛钱<br />
<br />
给他们？”伍皓的回答是：“那倒不至于，我素质没有那么低。”由<br />
<br />
此可见，在伍皓眼里，向他扔钱者的素质是“那么低”的。而又由于<br />
<br />
这一回答，后来不论他把自己演讲中被扔钱事件称之为“有趣的小插<br />
<br />
曲”，还是与向他扔钱的网友通过手机短信沟通，约定见面再次交<br />
<br />
流，甚至有与其“做朋友”的意味，都实在不过是作秀而已，难怪被<br />
<br />
称作“美女”的当事者之一的苏雨桐事后对记者表示，伍皓是在故作<br />
<br />
姿态，她也不认为伍皓答应跟到人大扔钱的抗议者面谈就是欣然赴<br />
<br />
约。<br />
<br />
 <br />
<br />
退一步，如果说在伍皓演讲时向他扔钱者就是素质“那么低”的话，<br />
<br />
那么，请问，那些不尊重宪法，逼得公民自焚、甚至跨省追捕举报<br />
<br />
者，他们的素质不是更低吗？然而，说句很让人高兴不起来的话，我<br />
<br />
们不少地方，不少政府部门，却正是用这种素质更低的人在管理着，<br />
<br />
在领导着哩（这可以从我们每年发生的多少有关“事件”中得到证<br />
<br />
明）。这一点，我想伍皓不会不明白、也不会不知道。据说伍皓之所<br />
<br />
以有此次被请到人民大学去作讲演的荣幸，主要是缘于他正在北京<br />
<br />
“进修”，也就是补某方面的课”吧。尽管我不知道他在“进修”什<br />
<br />
么，但我想，绝非是在进修民主法制观念。因此，在本人看来，伍皓<br />
<br />
更应进修的恰恰正是现代民主法制之课程，搞明白什么叫“普世价<br />
<br />
值”。你不能说中国一大群官员什么都不懂，但不少官员恰恰不懂这<br />
<br />
一点。而作为一个“政府人员”（伍皓自谓）如果这一点都没搞懂，<br />
<br />
确实不应该还跑到大学去作什么演讲。据我所知，在美国，即使贵为<br />
<br />
总统，也很难被学校批准到学校去给学生演讲，更不说一般的政府官<br />
<br />
员了。人家的理由是：担心总统的讲话中含有不适合孩子的政治内<br />
<br />
容。而此次被伍皓认为素质不高的向其扔钱者告诉记者的恰恰是：<br />
<br />
“之所以这样做，是认为一个官员去给一群学新闻的人讲课，会传递<br />
<br />
错误的价值和信息”――这真不知是巧合，还是虽国情不同，却人心<br />
<br />
一理。<br />
<br />
 <br />
<br />
自然，我不怀疑伍皓的“党性”，也不怀疑他对现实有那么一点正常<br />
<br />
的认识，但要谈民主自由，要谈尊重，要谈素质，他还差得远。<br />
<br />
 <br />
<br />
如果真的要说伍皓也还有些素质的话，那么，就是他毕竟明白网友之<br />
<br />
所以向他扔钱：这就是网民的抗议，而“有网民向政府人员抗议，这<br />
<br />
是很正常的”，“这是网民表达意见的一种形式”――中国有为数不<br />
<br />
少的官员可能至今连这一点都认识不到，会认为这是“犯上作乱”。<br />
<br />
 <br />
<br />
但通过新闻我们看到更多的不是伍皓如何有素质，而是素质的缺乏。<br />
<br />
 <br />
<br />
比如，在接受潇湘晨报记者采访时，记者问道：“有网友想问，这样<br />
<br />
的事情会不会导致出现跨省抓捕？”伍皓回答的是：“不会的，我们<br />
<br />
本身就在追求社会沟通，不断尝试透明公开，所以不存在跨省的问<br />
<br />
题。”看看，对这种不讲民主、不讲法制而无法无天的“行为”，在<br />
<br />
伍皓口中说得多么轻巧，不过一个“不会的”、“不存在”而已！他<br />
<br />
就认识不到，这不是会不会、存在不存在的问题，而是应该有制度保<br />
<br />
护，在一个社会绝对不允许发生的事。否则，我们可以设想一下，如<br />
<br />
果真的对扔钱者去搞什么“跨省追捕”，那么，什么民主法制，什么<br />
<br />
依法治国，什么人民的幸福和尊严，不都成了骗人的鬼话了吗？<br />
<br />
 <br />
<br />
我当然注意到，伍皓还说了一些很动听的话，比如：“这是个越来越<br />
<br />
开放的社会、越来越开放的国家。出现这种情况很正常，是表达意见<br />
<br />
的一种方式，但是这种行为还是不能成为常态，公众还是不需要<br />
<br />
的。”即使就是这么几句动听的话，也还是有些说得明白，有些说得<br />
<br />
不明不白。比如，说“但是这种行为还是不能成为常态，公众还是不<br />
<br />
需要的”，我就不明白是什么意思。像这种“事件”，在民主国家都<br />
<br />
没有“成为常态”，在中国也就更不可能还“成为常态”了。不然，<br />
<br />
一有人演讲，就有人扔鞋或是扔钱，那不扔反而会让人以为不是“常<br />
<br />
态”了，这将是不可思议的。此外，说“公众还是不需要的”，这是<br />
<br />
在拿公众作“挡箭牌”。没听说哪儿有演讲，对演讲不满者却会去向<br />
<br />
公众“扔”什么。既然不是向公众扔，也就谈不上公众需要不需要。<br />
<br />
伍皓的意思，无非是公众不希望因“扔”而影响了演讲会场的秩序，<br />
<br />
但我敢肯定，这不过是伍皓的想当然罢了。<br />
<br />
 <br />
<br />
此外，伍皓还说：“我们的社会需要更多的宽容，但我们每一个社会<br />
<br />
成员也应该不带任何偏见地看到国家的进步。我认为，体制内外应该<br />
<br />
形成良好的沟通和良性的互动去促进更大的进步，而不是对抗和对<br />
<br />
立。”这几句话说得当然好。但他也应该明白，扔钱者未必就是带了<br />
<br />
什么偏见没有“看到国家的进步”，他只是不喜欢你伍皓，不喜欢你<br />
<br />
一个官员去给一群学新闻的人讲课，难免传递错误的价值和信息。而<br />
<br />
不喜欢某个人，不喜欢某个人的某种行为，不等于就是带了什么偏见<br />
<br />
去看国家的进步。这没有必然性。当然，若是再从大的、深层次方面<br />
<br />
说，就更显得伍皓的肤浅了。此次到人民大学现场抗议的知名博客作<br />
<br />
者莫之许在接受媒体采访时就表示，（很显然）他们采取抛撒纸币的<br />
<br />
行为艺术作为表达方式并非针对伍皓个人，而是向一贯行为诡秘、不<br />
<br />
断以不公开指令控制言论表达自由的官方宣传体系表示不满。然而，<br />
<br />
作为曾经作过新华社云南分社常务副总编辑，后又任一省意识形态的<br />
<br />
副长官居然想不到“这一层”――当然，也不知是真的想不到，还是<br />
<br />
揣着明白装糊涂。</div>

							
							
							
							
