<br />
厚泽希望大家共同创造一种比较和谐融洽气氛，比较活泼宽松的舆论<br />
<br />
环境[7][7]，鼓励、提倡理论工作者开展创造性的研究工作，理论界<br />
<br />
要提倡独立思考，不同观点的争鸣和讨论，更要提倡批评和反批评都<br />
<br />
心平气和、实事求是、充分说理[8][8]。从而结束了「马丁事件」，<br />
