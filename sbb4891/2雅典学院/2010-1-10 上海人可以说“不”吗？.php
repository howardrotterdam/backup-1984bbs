													<h2>上海人可以说“不”吗？</h2>
						
						
																											<div id="postmessage_423180" class="t_msgfont">新年第一博，卡哥我打算用个脑残的标题吓你们一下，哈哈。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;还是在元旦前，网上热传一件事，就是上海电台的一个主持人在一档《音乐早餐》的节目里，读到一条听众短信：“求你们不要说上海话了，我讨厌你们上海人！”然后主持人说：“这位听众，请你以一种团成一个团的姿势，然后，慢慢地以比较圆润的方式，离开这座让你讨厌的城市，或者讨厌的人的周围”。结果在网上掀起轩然大波，引发了众多上海人与外地人的口水战。各大论坛连续几天硝烟弥漫、唾沫横飞，你能体会那种看打架不嫌事儿大的乐趣吗？反正我是看的津津有味乐不可支啊。开始的时候，正反双方还是势均力敌，慢慢到后来，随着砖家学者叫兽也加入战团，纷纷推出高度不同、背景各异、角度新奇、视野广阔的各类解读，舆论出现了一边倒的态势，都快演变成对上海人的一场网络大批判了。粗略总结了一下，大概有“素质低下说”、“地域歧视说”、“文化排外说”、“方言原罪说”、“话语霸权说”、“城乡二元化矛盾说”等，卷卷醍醐灌顶，篇篇高屋建瓴，真长学问啊。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;先说这事儿，要我说，这事儿本来就不是个事儿，鸡毛蒜皮，本质上和菜场里大妈小贩拌嘴有啥区别呢？就事论事，我其实挺理解那个小主持人的心情的。但国人就是这样，别看整天里嘴上喊着理解万岁换位思考，一轮到自己头上就没这回事儿了，天底下就自己受了天大委屈，有错儿的都是别人。其实最简单的道理就是“要想公道，打个颠倒”，你到人家上海人的家门口喊：“求你们不要说上海话了，我讨厌你们上海人！”然后你还指望人家怎么着？冲你乐吗？说：“是是是，您说的对，我们上海人就是讨人厌，我们再不说上海话了，省得您听不懂，上海欢迎您！”这恐怕您就高兴了吧？问题是，换你你干吗？我不知道发短信的人是某地人，但如果明天上海人到你老家门口喊：“某地人都是孙子！”，你能有人家这个小主持人这反应吗？反正搁我我是不干，歌里不是还唱呢吗？“朋友来了有好酒，若是那豺狼来了，迎接它的有猎枪！”你堵到人家家门口骂大街，挨了骂（还是很优雅的骂），然后满脸大便样的饶世界嚷嚷：“上海人排外！”――除了贱，我都想不出第二个字来形容你。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;再说方言，我是没有在上海生活过的人，但作为一个从小地方来到帝都生活的人来说，还是有些感触的。方言已经不仅仅是一种和当地人交流的工具，而成为洗净外地人泥脚杆，从而获得本地人接纳认可的一种身份象征了。尤其像北京这种大地方，一个外地人，如果你能学会一口纯正的京片子，那绝对能给你带来无尽的生活便利和发展机会。我在火车上曾亲耳听过一个四川在北京上学的女孩，跟她妈妈打电话时是一口地道的家乡话，突然一个电话插进来，这孩子马上变一口纯正的北京话，发音特标准，没有一个字是夹杂着外地口音的，小字眼儿咬的那叫一个顺，这时候要是有一个人说这个女孩不是胡同里长大的北京闺女，我都能跟他急。可笑的是，这路假洋鬼子北京话，说起来必定要比北京话还要北京话才行，仿佛非如此不能显示出自己本地血统的纯正。比方说句尾的“er”一定要比北京人的“er”还“er”；北京人有一句口头禅叫“您知道吗”，一般两三句话里能带出一句来，而半路出家的二茬子北京话说起来恨不得一句一个“您知道吗”，好像这世界上除他之外就再没有明白人了一样。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;我很同意周立波的一个说法，就是说上海人让一些外地人感觉到被歧视的，其实已经不是方言的问题了，而是那种“腔调”。这个词很有意思，概括出一些只可意会不可言传的东西，与北京人身上那种打娘胎里带出来的大爷“做派”类似，但也各有侧重。我个人体会，北京人的优越感侧重于对自己身份的自恋，换句话，与其说他瞧不起外地人，不如说他太把自己当回事儿了。爷这儿就是世界的中心，爷是老大，爷什么没见过？管你是谁，在别地儿再牛逼，来在这皇城根儿下，是龙你得给我盘着，是虎你得给我卧着，甭跟这儿耍牛逼，谁牛逼就灭谁。外地人跟北京大爷打交道都得顺毛捋，先把他端起来，供到那个神龛上，这种您高我低的心理优势一旦确立，北京大爷反而会待你热情似火，尽心尽力，大到国际谈判，小到街头问路，无一例外。上海人的优越感偏重于对“体系之外”的排斥，倒不见得一定感觉自己就高外乡人一头，但就算你是山西煤老板到上海商场里购物，收银员小姐也会瞥一眼你那装满现金的大编织袋子，相互会心的一笑，笑脸相迎，心里琢磨着怎么把你这乡下憨包的钱赚到我口袋里来。正像王小波笔下的德国人一样，他说德国人看不起人根本不用说的，两个女人眼神交汇一下就够了。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;在外地人看来，北京人牛什么啊？不就仗着是帝都吗？如果不是帝都你们算什么啊？比我们能强到哪去啊？上海人牛什么啊？比北京官大还是比煤老板钱多啊？没错，确实不强什么，而且北京上海也不仅仅是北京人和上海人的北京和上海，是全中国的北京和上海，在改革开放经济发展城市化的大潮里，每个人都有资格到这些地方淘金、工作、生活、扎根....但你不要忘了一方水土养一方人，一个地方的历史、地域、经济等综合因素造就了与众不同的人文环境，不管你喜欢或不喜欢，没办法，就是那样。周立波说他去东北，东北朋友非常热情，进门就拉他脱鞋上炕喝酒，上炕在东北是招待朋友的最高礼遇，就是拿你当自己家里人的意思。下次，这个东北朋友到了上海，我花钱请他吃最好的西餐、喝最好的咖啡、到百乐门大光明跳舞看戏……，然后这个朋友到了我的家里，有沙发不坐，脱了鞋就要上我和我太太的床，我说这个在这里是不行的，然后你就说我排外？<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;就在几乎所有正义之士都在口诛笔伐那个小主持人的时候，我在想象，发讨骂短信那位是个什么心态？作为一个金融危机之中在高物价高消费的上海捞生活的打拼一族，工作和生活的压力是可想而知的，可能当时正堵车堵的心焦，听个节目吧还是听不懂的上海话，又想起平时生活中上海人时时戳痛自己脆弱敏感自尊心的那种“腔调”，于是，新仇旧恨一起发泄，否则要憋到七窍吐血。那更多的普普通通的上海人呢？整天辛辛苦苦朝九晚五养家糊口，工资不涨房价涨，薪水不高血压高，对老板对客户要赔笑脸、拍马屁、挨训斥，回到家对老婆孩子偶尔才敢满脸阶级斗争――早晨上班路上听个节目还要受外地人挤兑？上海人在上海电台里说两句上海话，还要被外地人骂，凭什么啊？上海人又做错什么了？<br />
