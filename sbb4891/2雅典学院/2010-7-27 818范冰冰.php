　　但又不避讳的说，我之所以能在圈中这几年从片酬千元打拼到现在数万一集二线的地位，也主要是靠的献身。我之所以这样讲是实话实说，在圈中为献身出名不是什么丢人的事，但像范冰冰这样不管对方身份高低就因生理需要而献身的女星确实少见。和范冰冰有过肉体关系的除了影视公司老板，投资方，导演外，甚至包括场工。她也确实像一些爆料所说“人尽可夫”，在剧组里，只要她能看上眼的，不管是否对她事业有利，都会大玩一夜情。而她当年在《还珠格格》剧组时就曾被剧组知情人透露出“不干净”，而她也曾因此患过性病在北京某医院妇科就诊过。圈中的一位朋友透露说，当时范冰冰去医院只是做做常规检查，不想在检查中却发现患上了性病，而当时她并未走红，所以医院很多人不知道是谁，因此这件事情知道的也是极少数人。所以当听说范冰冰的经纪人为她争辩说范冰冰不是靠潜规则走红，是靠《还珠格格》的实力一炮走红的的时候，我真的感觉非常好笑！当年的《还珠格格》大红后真正捧红的人只是心如和赵薇，范冰冰一个配角能凭这部剧大红特红也太天方夜谭了，要说她真正走红也是这两年时间；只要进了这个圈子里你想清白都难，因为许多事情就不由你来决定了。在这个圈子里，当婊子不丢人没人笑你，丢人的是当了婊子还要立牌坊令人反感了。 <br />
