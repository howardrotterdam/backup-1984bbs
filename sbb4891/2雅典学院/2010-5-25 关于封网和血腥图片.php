4）又开始说历史，历史能等于现实吗？历史上汉族在那里的时候，某族还没产生出来呢，凭什么说那里就是某族的地方还要把汉族赶走？。 ... </blockquote></div>　　<br />
　　挑你一句值得一回的问题顺便说几句吧。<br />
　　关于汉族人比维吾尔人先到新疆，按照先占先得原则，那也何该是汉人的土地而不是维吾尔人的土地，这种说法现在倒是经常见到。<br />
　　我的看法是：<br />
　　第一、这种观点无意义，除非你认可殖民地逻辑，即汉族人在新疆的第一批移民遭遇回鹘人西迁至新疆，历史上汉族政权曾在新疆建立有效统治的时间早于回鹘人在新疆建立政权。而且，抬出这个逻辑时，显然是一种争论排他性的主权归属问题。问题是，新疆问题的主权归属问题是没有任何可争议的，不存在什么汉国与维吾尔斯坦国争议一块土地归属的问题，也不存在中国境内某个民族因为是更早的居民，所以拥有独立的充分理由。这种抬杠如非故意，起码是在帮助重复了疆独的逻辑。<br />
　　第二、这个说法与历史事实不符。<br />
　　这条可以稍微展开一下。维吾尔族得名的确来自回鹘，就民族延续来说，韦纥、乌纥、袁纥、回纥、回鹘、畏兀儿这些名字都是不同时期对一个特定突厥语部落的汉族译法。但维吾尔人等于当年的回鹘，这个说法就太过简单，好比说，今天的汉族人等于古代建立周王朝的部落一样。如果说，今天的维吾尔人主要是突厥语民族与新疆操东波斯语的雅利安人混血后的后裔，大致差不多。<br />
　　古代新疆天山以北和天山以南的经济形态完全不同，南疆绿洲上早期居民主要以农耕为生，从雅利安人到蒙古人种都有，而天山以北的牧场则是游牧民族，长期为突厥语民族，或曾为操东波斯语的塞人控制，譬如乌孙――乌孙的说法也有不同，但俄国人和考古的发现似乎更有说服力。汉族人第一次知道这些地方时，当时新疆整个都为匈奴人控制，但南疆多为操雅利安语民族，北疆是突厥语民族。<br />
　　在可考的两千多年历史中，整个新疆大部分时候为突厥语民族控制，中原王朝只在汉唐两个时代有过短期控制，有效控制时间并不明显比吐蕃人对新疆的控制时间更长。中原王朝控制时期，除驻军外，还在哈密吐鲁番等地有少量移民，今天研究古代土地制度时，新疆出土的唐代契券是重要的文献资料。但南疆的突厥化历程，要早于回纥人西迁，突厥的什么部族最早与当地塞种人混合，缺少可靠的说法，回纥西迁进入新疆的一支，最早建国于吐鲁番、乌鲁木齐一带。西迁新疆的回纥人只是回纥人并不多的一支，甚至未必比迁至山西大同地区融入汉族的人更多，回纥以迁移到葱岭以西的部落最多。<br />
　　准确地说，维吾尔人是从回鹘人那里继承了它的名字和突厥语，但放弃了游牧生活方式和摩尼教、萨满教，继承了当地塞种居民的农业生产方式和佛教的一个混合民族。除了西迁的回鹘和游牧新疆的突厥语民族以及天山以南的雅利安语民族外，还融入了羌人、藏人和汉人的成分。即使以回鹘人与塞人两个源头来概括维吾尔人，也不能说汉人早于维吾尔人的祖先抵达新疆，毕竟它狭义上的另外一半源头是世居的塞人。而且最早抵达新疆的汉人，早已融入了维吾尔人。<br />
　　如果依照回鹘人就是维吾尔人的逻辑，其实，今天那些在网上打字使用方言口语的南方人，在被北方人大骂“请用汉语不要用鸟语”时，大可反唇相讥：我说的才是真正的古代汉语，而你的所谓普通话，不过是胡人的语言，你们北方人原本就是蛮族后代，真正的汉人，早就衣冠南渡了。<br />
　　相对来说，汉族人成分的单向流动――周边不同的民族不断变成汉人――比起追溯游牧民族的源头要容易得多，游牧民族的高度流动性和政权部落的经常不断打破重组，使得套用现代民族观念描述一个历史上的民族或部落使，经常似是而非。比如一些常作为某个民族某个部落的名称，经常会在多个今天不同民族的今天找到地名或部落名的痕迹，因为一个部落很可能分散后，归属今天不同的民族，甚至是语言差异很大的民族，但依然保留有原名，像汉族历史上曾记载过的高车、铁勒、克烈、乌孙等等游牧部落名称，均在今天维吾尔地名中留存。一个部落崛起，则周围的全部部落对外统统被以核心部落称呼，回鹘与维吾尔大抵如此。<br />
