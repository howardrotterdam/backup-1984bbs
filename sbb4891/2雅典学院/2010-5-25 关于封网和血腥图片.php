																											<div id="postmessage_638924" class="t_msgfont">　　新疆封网的最直接动机是担心民族之间的暴力冲突由新疆蔓延到全国，但就这个目的而言，封网举动本身的象征和暗示，不要说丧失了其最初想实现的愿望，甚至起到了相反的作用。<br />
<br />
　　封网举动是最强烈的一种新闻封锁手段，在非常特殊的情形下，在获得授权的情形下，临时性地采取封网措施，是可以理解而且可以支持的行为。<br />
<br />
　　但是，就新疆封网时的局势而言，封网对防止本地暴力蔓延来说，是多此一举，而且是可疑的。因为７５事件发生的第二天，新疆局势已完全掌握在政府手中。除非７５事件完全是境外或境内有组织有准备的恐怖袭击，而封网可以阻断有组织暴力事件的联系通道，但７５事件后，尽管官方第一时间即宣称是热比亚操纵，但迄今抓获判决无数凶手，却无一证据证明是受热比亚支持。从切断恐怖组织来说，封网完全无意义。<br />
<br />
　　从封网切断恐怖谣言和仇恨煽动老说，效果则更为可疑。<br />
<br />
　　强力新闻封锁，在任何地方，都不能制止谣言，相反，它只是在人群中大规模制造谣言和恐慌情绪。７５事件前后相继的一系列事件，是新闻封锁强化和制造谣言以及制造民族仇恨冲突的最典型案例。它从一开始就是在新闻封锁和谣言中不断发酵起来的。将来有一天，在环境允许的时候，它应当成为新闻封锁与社会群体心理学的最好研究素材。而一年前拉萨的３１４事件，新闻封锁造就谣言和仇恨的因素，以及对事件的突然爆发的促进因素，则相对极少。<br />
<br />
　　７５事件的导火索是韶关冲突。在汉族社会，导火索起于维吾尔族工人多次强奸汉族女工但又多次被释放的谣言。在维吾尔族社会，导火索的燃烧时间更长，从新疆向内地输送年轻劳工开始就不断发酵。由于在基层，输送劳工过程中存在大量野蛮强制行为，且被输送劳动力又多为青年未婚女性，使南疆保守的维吾尔社会对此充满种种疑惧，虽然不少维吾尔知识分子支持这种中国特色的农业社会向工业社会的转型手段和方式，但只有官方正面歌颂的宣传，而完全禁绝媒体自由客观报道，使大量“古丽”被内地厂长经理霸占的传言在新疆广泛传播。<br />
<br />
　　韶关事件发生后，严格的新闻封锁，其制造的愚蠢效果为近几十年来所罕见。新闻封锁以及对网上汉族发帖者的处理方式，使汉族社会不少人相信维吾尔工人连续强奸被包庇是事实，而对牵动全民族关注的维吾尔社会来说，除少数认真分析视频的人认为，维吾尔人死亡人数不超过二十人外，普遍认为韶关有几十甚至二百人被虐杀，官方刻意淡化民族冲突的说法无一人相信。尤其严重的两个结果是，１、新闻封锁使汉族社会完全不知道韶关事件在维吾尔社会的愤怒和仇恨，因为根本听不到维吾尔人的声音；２、少数汉族人对韶关事件的幸灾乐祸在维吾尔社会被空前放大。<br />
<br />
　　７５事件屠杀发生几小时前的乌鲁木齐，人数不多的学生游行，除少量视频和图片流出，完全无任何新闻报道，虽然学生游行当即被早有准备的政府控制住，但枪声和开枪的恐怖传闻迅速在维吾尔社会传播发酵，而此时汉族社会的大部分人对即将出现的仇恨大爆发毫无准备。<br />
<br />
　　官方在６日凌晨迅速平定暴乱，控制局势后，含混而暧昧的新闻报道，不但让汉族社会陷入对维吾尔人的仇恨和恐怖当中，亦让自己成为汉族和维吾尔族的共同仇恨和不信任的对象。以为靠武力和新闻封锁即可一切皆在掌握的新疆地方政府，在７日全世界记者纷纷赶到时，出了空前的洋相：这次是忍无可忍的汉族人大规模上街，维吾尔人被追杀。因为他们被种种私下流传的种种血腥故事彻底激怒，认为官方的新闻完全是在愚弄和羞辱汉族人，抛弃了汉族人。<br />
<br />
　　用最严厉的新闻封锁，把全新疆搞成局域网，用最不着调的轻描淡写来缓和民族冲突，或许是官方对韶关事件视频流传的后果，按其习惯逻辑“吸取教训”作出的必然反应。它当然相信，韶关死了维吾尔人，导致乌鲁木齐死了更多的汉人，乌鲁木齐死了很多汉人，将在全中国死掉更多维吾尔人。这种逻辑用在民族关系长期隔膜，民族矛盾长期积累时，除了加深仇恨和猜疑，别无任何贡献。<br />
<br />
　　我相信７５事件中，官方报道的伤亡数字是接近真实的，但老百姓无论汉人还是维吾尔人都怀疑这个说法。即使是在维吾尔社会，也有人认为，７５当天汉人起码死了一千人，至于维吾尔人的死亡失踪数字就更大。但从有限的调查看，传言中维吾尔人当日的死亡人数是被严重夸大了。中国官方的新闻从来以不可信著称，在两个民族集体陷入空前恐惧的时刻，事实和真相是人们最迫切的需要，中立的非官方新闻机构的缺位，不但给了谣言最大的市场，亦使全社会陷入空前的不信任当中。<br />
<br />
　　新闻封锁，有时竟在文化截然不同的民族中造就完全相同的留言和恐惧。９月７日，乌鲁木齐汉族社会和维吾尔族社会同时流传一个恐怖事件：华菱超市的几千把斧头被人买光或被抢光，８日晚上对方民族将会搞大屠杀，它果真在那个晚上让乌鲁木齐万人空巷。当时的传言当中，汉族传言与维吾尔族传言惟一的区别或许是，汉族社会还传说乌鲁木齐的硫酸或硝酸被维吾尔人买光了。<br />
<br />
　　而在８月这个乌鲁木齐“集体扎针月”，月初汉族社会就开始断断续续有维吾尔艾滋病人扎针的传言，到了８月中旬，政府含混不清的避谣和新闻封锁，使扎针恐慌迅速蔓延，这种此前曾在北京、上海、天津、西安、乌鲁木齐发生过社会集体癔症，在特殊气氛下在乌鲁木齐造就了空前的恐慌，而维吾尔社会同样也在流传汉族人专对维吾尔小孩子扎针的传言。２４日前后，手机用户集体收到的一则官方消息，让此前不敢对公众大声说出自己怀疑的专家也陷入疑惧，此前的调查表明，医院收治的被扎针者找不到针眼也找不到被扎的证据，现在，官方发布的短信和抓获维吾尔族扎针者的新闻使他们彻底崩溃无言。或许是出于某种其妙的心理补偿，维吾尔社会则在８月底开始流传多起汉族妇女头戴围巾伪装维吾尔人扎针被拿获后却悄悄释放的故事。<br />
<br />
　　７５事件新闻封锁造就的最离奇传言，是内心羞愧的一些维吾尔人相信，之所以那些屠杀汉人的维吾尔人如此丧尽天良，是有安全局的维吾尔人装扮成普通人，首先袭击汉人，煽动了那些暴徒。这种缺乏起码常识的传言广有市场，是因为维吾尔人几乎不敢公开议论和打听７５事件。而汉人社会中与之类似的王乐泉一开始故意纵容维吾尔人闹事以证明新疆需要他的铁腕、结果局势失控的传言，同样是对７５事件前后详细过程新闻封锁的产物。<br />
<br />
　　告之人们事实的残酷，远比粉饰事实对社会的稳定更重要。因为前者是解锁的开始。直到今天，汉族人未必知道维吾尔人的仇恨从何而来，维吾尔人不知道汉人的仇恨从何来而。当然，渴望生活在一个有希望有安全的社会，是人们最本能的心理需求。所以，经历了７月７日恐怖洗礼的维吾尔社会也有个奇怪的看法和传言：那些袭击了维吾尔人的汉族人，几乎都不是本地人，甚至很可能主力是政府从内地专门运来的暴徒。这种看法，或多或少是７５事件后对本民族前途彻底陷入绝望的维吾尔人一种自我安慰：至少本地汉人不是那么仇恨我们，今后维吾尔人还能生存。<br />
<br />
　　至于血腥残忍的屠杀照片。无论从新闻伦理还是从社会作用而言，在任何文明社会都是禁止在公开出版物中传播和张贴的。即使是天朝，过去意识形态的宣传中，也极少用到自然主义手法拍摄的血腥残忍照片，而多用绘画方式表现。只是在１９８０年代中期，受日本修改历史教科书和参拜靖国神社刺激――天朝当时上下其实都不知道那些教科书的具体情形，多少因为自己的原因被日本左翼“误导”，开始大量展出残酷血腥照片。<br />
<br />
　　从新闻伦理角度而言，诉诸最直接感官刺激的残忍照片，是对死者及其家属的不尊重，是对文明禁忌的冲击和践踏；就社会作用而言，直接感官刺激的残忍照片，除简单刺激人的仇恨情绪，增加人的残忍，别无其他作用。无论是对同一国家的不同民族，还是不同国家的不同民族，用自然主义方式宣扬和展示其个体的暴行，均非文明群体可以选择的方式。历史上，日本人将通州事件中被杀的日本平民的血腥照片大量公开刊载，本意宣传“支那”的暴虐，结果令当时离开军营暴走的士兵，在中国成了更残暴的野兽。而天朝多年来习惯展示日军暴行的那些血腥图片――南京大屠杀的一些照片其实是通州事件照片，使南京大屠杀虽时隔７０年，依然让今天的中国人看后，一想到今天的日本人，就觉得其斯斯文文的外壳里边包裹的依然是一个野兽。所以，先杀日本男人，再奸日本女人，是今天中国人少数能赢得共识的理想之一。<br />
<br />
　　每一代人埋下的仇恨种子，都会让无辜的下一代人承受。天朝的前三十年，奉行的是让所有人仇恨所有人的教育，今天我们的收获，不消去看民族之间的冲突，只要看看商务印书馆的那个视频里，因为对伟大领袖毛主席的不同看法，那些老同志怎么对待年轻人就知道了。我觉得，做一个负责任的成年人，如果你煽动所有人仇恨所有人，你就有义务永远生活在所有人与所有人战争的中国，反之，你才有足够的资格说，老子肉体翻墙，去做个米国人。<br />
