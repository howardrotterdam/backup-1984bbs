OMG，不高兴的中国简直不相信自己的眼睛，贞德来了，刘胡兰来了，林昭来了。果然，这条消息如杨枝甘露，润泽了民*主男们喊口号的嗓子眼。民*主群沸腾了，有人激动地说，终于有人为《宪章》流血了。多直率多坦诚，民*主男们盼星星盼月亮，就是等人为《宪章》流血。我顿时就纳闷了，这么一部《宪章》，虽然有其价值，但也完成了它的使命。首先，它让刘X晓X波坐上了牢，更接近诺贝尔和平奖了。其次，它引起了国际社会的关注，某种程度制约了中*G对民X运人士的打压。除此之外，《宪章》还有什么价值吗？写的又长又臭，跟政府工作报告差不多，知识分子看了都昏昏欲睡，就别说普通百姓了。冒着被抓起来的危险去街头派发有意义吗？这给刘X晓X波也添乱啊，《宪章》的焦点一下子从他身上转到了刘沙沙身上，万一诺贝尔奖本来是给了姓刘的，最后却给了沙沙，这怎么得了？！<br />
&nbsp; &nbsp; <br />
我左思右想，就是想不通为什么中国民*主男们要为刘沙沙叫好。然后网上搜索刘沙沙，看的我顿时花容失色，这位沙沙要为《宪章》流血，恐怕和民*主无关，而是打鸡血洒狗血。此沙沙出身左翼家庭，父亲是毛左，她从小受到狂热的政治教育。她说自己有百分之八十是马克思主义者。这种马克思主义者看起来患有被迫害妄想症。她去年在天涯被封了ID，竟然以自杀相要挟，演足了“一哭二闹三上吊”。最终斑竹们不堪其扰，请辞去也，女烈士没死，神采飞扬，屹立不到。<br />
&nbsp; &nbsp; <br />
在网上回看刘沙沙的辉煌历史，以及众多网友对此“自杀门”的嘲弄，耳边仿佛传来了一曲《血染的风采》，我几乎潸然泪下。刘沙沙刘沙沙，为什么你去年不死，去年是多好的日子啊，去年的烟花特别多……<br />
