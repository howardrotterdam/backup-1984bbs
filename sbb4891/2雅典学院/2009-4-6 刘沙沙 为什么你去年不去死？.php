让我们血性而又理性，坚定而又温和，此起彼伏、前赴后继--野火春风、春风化雨、雨夜听潮：让人*权的潮水慢慢地、慢慢地上涨，让“文明”的细雨慢慢地滋润万物，给各方时间去妥协、去学习，给政府时间让他习惯--习惯那点点野火一样处处冒出的党派，习惯那一次次游行、一次次请愿之后的谈判，习惯那罢工罢课之后对穷苦员工的妥协，习惯我这样，漫天里纷披而下的雪片传单。<br />
