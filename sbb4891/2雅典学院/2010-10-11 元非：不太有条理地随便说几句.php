													<h2>元非：不太有条理地随便说几句</h2>
						
						
																											<div id="postmessage_865551" class="t_msgfont">不太有条理地随便说几句<br />
从 延宕与悬置 作者：元非<br />
有 2 人喜欢此条目 - 您，以及其他 1 人<br />
共享者：您<br />
<br />
刘晓波得奖后，冉云飞之类的入门级右派们自然一片欢腾。但豆瓣上好些以最高的标准来看头脑也并不单纯，对“民主”、“自由”之类的概念及概念背后的历史和现实有着深刻反思和体认的人们，也为刘晓波获奖而喜悦，并与其他人分享着自己的喜悦。这就有点意思了。<br />
<br />
参与或围观过网上论战的朋友应该都能体会到，无论对一件事有怎样的看法，或对同类事件持何种立场，都有某种智识资源可供利用，能够借以编织一套说辞来正当化自己的立场，并反对异己。而每种学说都必然有自己的premise，如果有人从一开始就不承认你的premise，说你独断，那是没什么办法解决分歧的。又如果某种主张的理据本来就失之浅陋，不招那些从智者派到后现代都了然于胸的高人待见，那似乎是一定的了。<br />
<br />
刘晓波恰好就是这么一位，学理粗糙，逻辑简单。从他这二十多年几无长进的文章，以及他最后留给我们的零八宪章来看，其粗糙和简单可以说是无可救药的。那么，豆瓣上这一部分心思缜密的未来××学家们，为什么会如此支持刘晓波获奖？<br />
<br />
格雷厄姆・格林在《文静的美国人》里借主人公的越南助手之口说， “迟早，一个人不得不拥护一边。假如他要继续做人的话。”于是，虚无厌世，一直在战争中作壁上观的主人公，为了避免更多平民在恐怖爆炸中伤亡，最终选择了参与暗杀那位制造恐怖事件的美国人。<br />
<br />
而对于以学术为业的人们，用沃格林的话说，韦伯式的所谓“价值中立”也站不住脚：<br />
<br />
&nbsp; &nbsp; 在韦伯的语境中，变得清楚的是，如果社会科学想成为一门科学，就不得不保持价值中立。对韦伯来说，那就意味着，社会学家不得不探索社会过程中的因果关系。它用来选择这些材料的价值观是前提，不被进行科学处理；因此，价值判断不得不排除在科学之外。这就给他留下了困难，即，运用于科学的材料选择的前提，以及一种责任伦理的前提，皆不得不呆在阴影中。……如果说，韦伯从来没有沦落为某类相对主义或无政府主义，那也是因为，即使没有进行这样的分析，他还是一个有着坚定伦理品格的人，并且，他事实上是个神秘主义者。因此，他知道什么是对的，而无需知道对的理由。当然喽，就科学而言，那是一个非常危险的立场，因为学生们终究想知道，为什么他们应该以确然的方式行动的理由；而一旦理由不予考虑，情感就很容易把你带入各色意识形态的和理想主义的冒险，而在那样的冒险中，目的变得比手段更激动人心。<br />
<br />
所以你看，虽然罗斯福的新政加剧了大萧条，加强了中央权力，一定程度上侵犯了财产权，干扰了司法，埋下了不少祸根，但我们若回到1941年，当德日站在文明世界门口的时候，恐怕还是会和罗斯福站在一起。一个可能更有争议，也因此更能体现这种意义的例子是，胡适1948年底飞离北平，选择了蒋介石。<br />
<br />
而刘晓波还远没有上面二位那般不堪。他的粗糙简单，并不等于错得离谱。相反，他大体正确。无论是他的愿景，还是他希望实现其愿景的路径，都没有大规模压迫，甚至血流成河的计划，这与20世纪的诸多社会实验迥然不同。他本人也言行合一，置完全可得的安逸生活于不顾，多年来生活在距体制最远处，坚持非暴力的不合作，并数度入狱。因为所有这些，他的人格值得高度尊重。当然，或许会有不逊于他的人选。但他显然比此前的一些获奖者更有资格得到承认，比如戈尔和奥巴马之流。诺贝尔和平奖也不是学术奖，没有理由用学术的标准去衡量刘晓波。否则，马丁・路德・金和曼德拉同样需要质疑。<br />
<br />
如果有人不屑于评论刘晓波，他/她大可不表任何态。但恶心的是，有些人非要谈，但又摆出一个超越的姿态来谈，字里行间满是一股子“我怎么才能显得比别人更高明一点”的臭味。我们是人，不是神。最高的真理在神的手中，不在世间。不管信不信神，都不应自以为神，装超越――也就是说，僭越。对吧。<br />
<br />
最后，我要充分利用博客位于墙外的优势，谨以此文祝贺刘晓波先生获得2010年诺贝尔和平奖。</div>

							
							
							
							
