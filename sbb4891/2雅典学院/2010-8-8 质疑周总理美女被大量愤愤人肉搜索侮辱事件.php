在今天看来，毛泽东当时发动的文化民主革命，现在却证明是无比的正确。民主是防止腐化的重要武器，民主是社会持续发展的根本保证和动力。在今天看来，毛泽东超越了历史，只是当时国民的文化素质的局限性，阻碍了民主发展，扫除文盲只能是文字的认识，那代人思想的文盲是无法扫除的，那时代的人们的思想无法跟随毛泽东&quot;伟大的思想家&quot;，只要是毛主席号召的坚决拥护和支持，这就是人民的悲哀，人民的权利是需要自己争取的。中国人民是最勤劳的人民，只知道劳动和奉献，不知道索取，索取自己作为国家公民的权利，这就是封建社会两千年的封建遗毒，而文化民主大革命被一小部分人利用了，破四旧是无法破除人们内心的封建思想，人民太依赖于迷信封建帝王的明君，失去人民对权力的监督，历史中的朝代不断更迭或重复。<br />
毛泽东晚年对自己的总结，自己一生只做了两件事，一件是把蒋介石的反动政府打倒，赶到台湾岛去，第二件是发动了文化民主革命。他知道自己的生命终将终结，&quot;一万年太久，只争朝夕。&quot;他要赶在生命的终结前完成，&quot;宜将剩勇追穷寇，不可沽名学霸王。天若有情天亦老，人间正道事沧桑。&quot;不管如何，为了中华民族的利益和长远发展，必须要勇敢的走出这一步，不管当时的时机成不成熟，人的生命只能有一次。<br />
北斗陨落，亿万民众悲痛流泪，这是内心的悲痛无助，是人民失掉主心骨的依赖和彷徨，毛泽东完成了把中华民族推上世界的民族之林，为世界无产阶级指明了革命的方向和方法，&quot;毛泽东思想&quot;是人类社会宝贵的遗产和灯塔，毛泽东是久经考验的无产阶级革命家，民主斗士，倡导全心全意为人民服务的思想，捍卫大部分与多数人的利益，普世价值的典范，必将与日月同辉。<br />
<br />
毛泽东虽然离我们而远去，但他的博爱，大公无私和思想却永留人间。他的时代结束了，但他发动的文化民主革命却远没有结束，随着时间的推移却越来越显示他的正确性和前瞻性！！！<br />
