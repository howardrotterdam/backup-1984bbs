 先说说我的身份，本人煤矿工作20余年，从事煤矿救护工作十来年，曾任煤矿救护队技术副中队长，多次参与或带队处理煤矿井下事故，有在综采工作面氧气浓度仅3.8%的灾区佩用四小时氧气呼吸器工作一个多小时的救灾灭火经验，大学文化，高级工程师。<br />
　　　　<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;1、关于钻机钻孔的技术问题。大口径钻机(打的钻孔钻杆口径是210mm)参与救援其实有许多难题，如终孔点的定位问题，把一个几百米的钻孔的终孔点精确地定位在一个宽度不超过3米的巷道上，其钻孔点的精确确定是难度极大的，王家岭煤矿距地表垂直距离最近的巷道是回风巷，270m，而且其人员位置并不一定在井口附近，更增加了钻孔定位的难度。而且，在井下环境及其恶劣的情况下，矿工能找到钻头装置并系东西在上面，一个数百米的钻孔，钻孔深度也并不怎么确定的情况下，钻头不偏不斜不高不低正好打在巷道里，而且能让矿工系东西，是不是有点天方夜谭的味道？<br />
　　　　<br />
&nbsp; &nbsp;&nbsp; &nbsp;关于钻孔救援问题，若干年前俄罗斯搞过大口径钻孔，那是专用的钻机，是通过钻孔向井下输入氮气灭火，钻孔定位当然是难题，钻杆钢度不够或钻杆直径不够大，那钻杆会象面条一样偏移终孔。而且王家岭煤矿的地质资料是否准确齐备也是个问题，因为那钻孔的准确定位是以准确的标高和经纬度为前提的，没有这些一切都是空谈。<br />
　　　　<br />
&nbsp; &nbsp;&nbsp; &nbsp;有人说只要有准确的图纸 不是什么难事，但王家岭煤矿的打钻孔救援此前的前期准备工作并没有详细的报道，如地质资料是否详实准确，地质部门打钻孔位置的确定和测算经过等，这些都需做细致的工作，王家岭煤矿的地质资料有是否详实齐备，新闻媒体没报道，大家也不知道。<br />
　　　　<br />
&nbsp; &nbsp;&nbsp; &nbsp;我是搞工程技术的，违反国内技术条件和科技水平的说法本身就令人怀疑，因为救灾情况的真实性关涉公共安全，涉及到公共利益和公民财产和人身安全，追求真实归根结底是人类追求自身安全的需要，决不允许违反常识和国内的技术条件搞虚假报道，也决不允许有人利用救灾搞政绩工程，2008年四川地震中出现了所谓9岁的林浩带伤救人(3人)的报道，后被全国很多人证实是虚假报道，因为一个无民事行为能力的9岁的林浩救人是违反常识的，让这样的人在地震时带伤救人几乎比登天还难，林浩救人被证实是虚假报道后现在也不了了之了，新闻单位从来不对其报道的真实性负责，从来不向民众道歉，因为自己揭穿自己的谎言自己打自己的嘴吧子比什么都难，况且这样的谎言的揭穿也没那么简单，因为它涉及一种行政法上的国家行为，那ＣＣＴＶ本身就是国家办的，它从来不允许有第二方，第三方声音的人员在媒体讨论和怀疑9岁的林浩救人的真实性，它从来都是一个调子，一个声音，听不到第二方、第三方声音，听不到第二方、第三方声音的新闻不是新闻，充其量是一种宣传。<br />
　　　　<br />
&nbsp; &nbsp;&nbsp; &nbsp;报道中的“王家岭矿难发生120小时后发现井下有生命迹象，救援人员通过2号孔道送下去360袋营养液，还捎下去两封信和笔、纸、电话，至截稿时，地面仍在焦急地等待着井下的回音”这种说法的真实性也急切证实。因为国内煤矿工程技术，目前还没有达到这种钻孔定位送水送信的技术水平，那几台钻机是什么型式的钻机，是不是专门用于救灾的钻机，现在从报道方面分析从来没有说是专门用于救灾的钻机，不是经过特殊研制和制造的用于救灾的专用钻机能“送下去360袋营养液，还捎下去两封信和笔、纸、电话”我做为一个矿山老工程技术人员对此种说法严重怀疑，因为根据我的跟综掌握国内的救灾技术目前还没有达到这种水平，更不用说一般的钻机了，此前国内也没有类似的成果或救灾实例。<br />
　　　　<br />
&nbsp; &nbsp;&nbsp; &nbsp;请大家注意，王家岭煤矿井下巷道距地表最近的垂直距离是回风巷，为270m，打这样的钻孔可以称为长距离钻孔了，请大家注意，我是搞煤矿工程技术的，若干年前我国在煤矿搞大口径1000m超前钻孔的科研项目，被称为具有世界前沿水平，而且这样的具有国际前沿水平的钻孔其钻孔开口口径是500mm的口径，也就是说钻孔的口径是不一样的，越深或距离越长口径越小，钻孔的终孔口径已经相当小了，当然那种钻孔是水平钻孔，难度更高。王家岭煤矿所用钻机的钻杆直径是210mm其实巷道定位等也存在诸多难题，它们用的不是专用钻机，可他们竟然搞成了，而且还“送360袋营养液，还捎下去两封信和笔、纸、电话”，我是严重怀疑其真实性。<br />
