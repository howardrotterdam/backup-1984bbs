&nbsp; &nbsp;&nbsp; &nbsp;我感觉，油田钻井并不存在定位问题，因为那达到要求的范围很大，那是向一个所谓油田的大范围内打钻孔，而且终孔也并没有太大的要求，只要深度够，按一定的方式方法排列，打到油层或煤气层，即使深一点也没什么关系。大家可以讨论一下。<br />
