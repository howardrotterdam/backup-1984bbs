可是，神奇土地之所以“神奇”，就在于发生的许多“奇迹”都让人意料不到，这不，4月7日，在第八届华语文学传媒大奖的“网络时代的文学促进”沙龙上，一批身处传统文学界的著名作家谈及有关网络文学的话题，总算有人发出了“消灭网络”的声音，且不再遮遮掩掩地冠以任何漂亮的理由。媒体报道写到：作家麦家在成都近乎咬牙切齿地说：“如果给我一个权力的话，我就要把网络消灭！”尽管对网络文学的质量实在不能苟同，但他坦言，他说这话是基于自己将被网络淹没，进而被网络作家打败的担心。<br />
<br />
“如果给我一个权力的话，我就要把网络消灭！”如果这话语出自神奇土地的官员之口，我丝毫不会感到奇怪，因为“雷人官腔”早已经见识了不少，可出自一个因作品《暗算》、《风声》等而风头正劲的作家之口，倒猛然间让我惊诧莫名。我甚至愿意相信“消灭”这话语只是麦家在沙龙上因沉溺于“暗算”、“风声”而走火入魔发出的呓语，甚至愿意相信这话语更可能是麦家以作家的笔调在冷静客观地描述着一个正在发生或将会发生的严酷现实。然而，遗憾的是，媒体报道的白纸黑字清晰地表明了麦家的真实想法就是“基于自己将被网络淹没，进而被网络作家打败的担心”。稍稍打开思路再一想，此前，作家们已不经意间创造出了“秋雨含泪”、“兆山羡鬼”的现代成语，现在再多一个更加赤裸裸的“麦家灭网”又有什么可值得大惊小怪的呢。<br />
<br />
实际上，之所以有人悍然发出“给我一个权力，就要消灭网络”的叫嚷，就在于神奇土地上“一朝权在手，为我所欲为”的无边现实，就在于权力的傲慢正在一次次地扼杀着良知，将社会良知的底线一遍遍地往下涂抹，以致几近黄钟毁弃而礼坏乐崩，自然就只会剩下诸如“纵做鬼，也幸福”、“给我一个权力，就要消灭网络”此类的瓦缶齐鸣。<br />
<br />
作家当是知识分子的一员，知识分子当是社会的良知，本该用自己的文笔和思想去扬公平、张正义，去揭伪善、灭邪恶，用自己的言行去捍卫社会良知的底线。然而，诸多的事实一次次地证明，社会良知正成为傲慢权力的祭品，知识分子良知的缺失也正在滑入加速度的进程――现代成语“麦家灭网”的产生是又一个证明。如今，知识分子竟然堕落到为了一己之私利就公开宣称欲用权力消灭网络上的异己，呜呼，这究竟是文化的悲哀还是社会的悲哀？<br />
<br />
不过，仔细一想，“麦家灭网”还是有值得称道的地方，因为它已不再犹抱琵琶半遮面，让人能了然知道其用心何在，这实在比那些扭扭捏捏欲盖弥彰的东西坦然不少。因其坦然，让人足可对其提前保持戒备，如果有选票当然不会投给他。为此，有人庆幸于好在现代成语“麦家灭网”的创造者尚不具有灭网的权力，网民依然还可继续互联互通思想互达。可是，随着不断的良知缺失和权力傲慢，谁有能断然否认“消灭网络”不会成为网民明天的梦魇呢？<br />
<br />
所以，如果某一天网络突然离网民而去，我不会大惊小怪，因为我知道，消灭网络，并不只是某个作家的梦想……<br />
