&nbsp; &nbsp;&nbsp; &nbsp;2、这是一起透水事故，而且时间已过了若干天，井下极其潮湿和脏乱，衣服和手碰到哪都是泥，但大家看到没有？那些直接从井下抬着矿工从回风井出来救援的人员的衣服有多么光鲜，多么干净？这符合常理吗？是不是POSE“摆拍”出来的？<br />
　　　　<br />
&nbsp; &nbsp;&nbsp; &nbsp;大家注意一下，救护队员穿的是服装叫战斗服，他们从回风井出来那战斗服确实很光鲜，回风井，即使让那污浊的风流过一下，战斗服都很脏。<br />
　　　　<br />
&nbsp; &nbsp;&nbsp; &nbsp;并且，大家知道吗？在井下用担架抬人，四个人，那是很累人的，一会就汗流满面，但大家看到的那些救护队员从回风井出来时，不仅战斗服很干净，而且并没有出多少汗。<br />
　　　　<br />
&nbsp; &nbsp;&nbsp; &nbsp;我曾经多次参加井下救人抬人工作，我们一般是让井下工作单位现场的矿工抬，因为一个小队只有九人，如果再做其他的工作，人手是不够用的，所以我们会借助现场单位人员的力量，抬人由他们抬，到地面后再换过来，这抬人的工作相当的累人，没做过此工作的人是不知道的。<br />
　　　　<br />
&nbsp; &nbsp;&nbsp; &nbsp;这句话我补充一句，在王家岭煤矿的救援中，这种担架谁帮他们抬呢？这种帮他们抬的人是绝无仅有的，只有他们自己动手，自己动手衣服不脏，没流多少汗，没有累得气喘嘘嘘身体发软，是很奇怪的事情。<br />
　　　　<br />
&nbsp; &nbsp;&nbsp; &nbsp;而且王家岭煤矿是斜井，当时回风井出来的人是斜井人车把他们一起拉上来的也不现实，因为，当时井下并没有恢复用电，而且很重要的一点，那是回风井，在煤矿中，回风井做人员升入井井筒是不允许的，回入井筒甚至不允许大量的电源及电器机械设备的存在。<br />
　　　　<br />
&nbsp; &nbsp;&nbsp; &nbsp;大家注意一下，那救护队员身上背的东西叫氧气呼吸器，以前是负压的，现在大多数煤矿的救护队员都改用正压氧气呼吸器了，他们身上背的东西我是相当的熟悉的，若干年前我们国内刚引进的时候，我在重庆参加救护年会的时候还听过德国专家的介绍，当时一台德国进口的是一万五千美元，现在国内仿制的厂家已多如牛毛了。大家注意了吗？他们救援后的氧气呼吸器也很干净，这就很奇怪的，背着那东西救护不碰东西是不可能的，而且是透水事故，那氧气呼吸器碰到哪都会沾上呢，但大家看一看，那些氧气呼吸器都很光鲜，甚至象没有下过井的样子。<br />
　　　　<br />
&nbsp; &nbsp;&nbsp; &nbsp;再有，这里告诉大家，背着氧气呼吸器抬人，那劳动强度绝对是高强度的活，几乎类如快速地爬山，一会你就会受不了，我在这里给大家一个参考数据，一个人小跑的体力输出相当于中等劳动强度，那背着氧气呼吸器抬人的活绝对是比中等劳动强度高两个等级的体力输出，是超高的劳动强度，但看那些队员却很轻松的样子，一是流汗不多，战斗服不脏，身上背的氧气呼吸器没有泥，并没有想象中的那样劳动强度的样子。<br />
　　　　<br />
&nbsp; &nbsp;&nbsp; &nbsp;3、更令人怀疑的是，矿工的生理问题，长胡子是矿工生理现象，谁也抗拒不了，有人一天不刮胡子就受不了，几天不刮感觉就成了“黑李逵”，可是王家岭煤矿救援上来的矿工照片和视频显示却象胡子刚刮过一样，也就是说这些矿工经过了将近十天的时间没长胡子？自然规律被这些矿工打破了，真有这样的奇迹？<br />
　　　　<br />
&nbsp; &nbsp;&nbsp; &nbsp;请大家注意，那些矿工可是直接从井下，而且是从回风井抬上来的，并没有经过医院医务人员的擦洗和刮脸，没长胡子也着实令人怀疑其违反生理现象，违反自然规律，一个没长，二个没长，若都没长就更是明显违反常识和自然规律的不实现象。这些都要有第三方更多佐料的证实，谎言真不了，前苏联《真理报》只有一家真理除日期是真的没有一句可信的时代毕竟过去了，但它的影子还在，所以我们要在理有据地怀疑，小心地求证。<br />
