您这话真令我唏嘘不已。我N年前眼睛出现偏盲，找遍了北京协和、同仁的国内顶级眼科专家，检查了一遍又一遍，都找不出问题，最后诊断书说我是癔症！随后就 ... </blockquote></div>你说的问题很普遍，但我认为这个现象最大的原因与其说是临床经验的差别，不如说是医疗卫生体制的差别，具体有三个原因：1. 养和医院是私立医院，香港和欧美的私立医院有两种医生来源，一是来自于公立医院的资深大夫，为了更好的待遇和更宽松的工作环境进入私立医院，再就是一些没有取得专科执照的医生和一些想清闲的医生，自己开门诊做家庭医生。一般后者是个体门诊，而像养和这种中大型私立医院主治大夫的来源主要是公立医院的资深大夫。所以在香港，社区医院的大夫素质是不太好的，因为这些地方的大夫又好多是刚开始转科的大夫，类似于国内三家医院里的住院医师和实习医师。2。香港的转诊制度：在香港和其他某些发达国家，就医制度是首先去政府制定的社区医院看病，尽管政府不会强制你去那里看病，但会通过很多优惠政策间接地把病人吸引到就近的社区医院，如果社区医院觉得有需要，就会把病人介绍到相应的综合医院和专科医院。这样就避免了门诊量像国内三甲医院的门诊量那样变态的多，三甲医院大夫的工作量长期超负荷。在如此超负荷的工作状态下，医生的业务水平和责任心肯定会受影响。3. 医患关系，根绝我的亲身体会，是影响医疗质量的很重要的因素。如果不做详细检查就开药，那病人会觉得大夫不负责，但如果把该做的检查都做了，病人也有可能会觉得大夫吃提成，在在检查的时候，很多检查结果不可避免的是阴性结果，但这不代表这些检查是不必须的，但是在国内的医患关系下，大夫很难对每个病人都详细的解释，就是解释了，很多病人也很难理解，这样的结果就是，大夫宁可省的心少做甚至不做检查，而只是根据个人经验开一些保守的药物，尽管效果不一定理想，但是大夫能保护自己。4。就是国内的医保制度，病人很难有经济能力完全负担所有该做的检查。<br />
综上所述，并不是国内的大夫临床水平太差，而是整个医疗体制无法把病人合理的分配到合适的大夫和医院里去，社保制度无法有效的保证病人的看病权利，法律和保险制度无法有效的保证大夫的工作环境安全甚至人身安全。<br />
所以有一个现象，不少国际上顶尖的手术技术是国内开发的，但是国内的医疗体系却无法保护大多数人的医疗权利。</div>

							
							
							
							
