																											<div id="postmessage_305979" class="t_msgfont">前些日子得了肺炎，在香港公立医院体验了啥叫公费医疗。第一次去，挂号费100。发烧39+，医生直接给了我特敏福，X光显示肺部阴影，让我住院，我签了个字没住。回去呆了两天吃药无效，感觉要死，就又去了，直接住院，决心已下带着电脑去的。让我在普通病房呆了5分钟左右就直接转隔离了（怀疑我是肺结核，当时我死的心都有了，当然也确实快了。）隔离病房相当高科技，两层防护门（不能同时开启），红外感应开门，房间内气压很低，空气在开门时没有流出的可能，房内有摄像头一枚（睡觉都看着。。）。独立卫浴，每天有专人进来清洗。房间内有电视一枚，四个免费频道（有个什么宫心计我看到吐）。床头有两个水壶，一个热的，一个凉的，专人负责保证满。每天三顿饭，早上肉糜粥，不错。中午是不同的肉，加一道白水煮菜，本人已在香港生活数年，对于这种低劣的食物还是可以忍耐的。晚饭同午饭。每天三针抗生素（11点，5点，夜里3点），成分阿莫西林（我自己就有，觉得对肝不好没吃，结果进来直接打）。各种人为我忙来忙去，有事就按铃，人家摄像头里看我一眼，说“你把口罩戴上”，很快就有人进来了。最讨厌的是一个带面具进来的人，人家都戴口罩怎么就你怕死，丫每次进来我都觉得我确诊了。在隔离期间，各项病征符合结核感染特征（抗生素无效，肺上部感染，晚上开始烧，烧到要死，我下午还乐呵呵看连续剧呢）。终于在大家隔着门来看我那天晚上好转，不是肺结核！。之后我被转去去了可悲的非隔离病房，8人一间，其他人都是70+的，只有我。。三年来一直引以为豪的免疫系统和我开了个玩笑。每天的生活就是玩电脑(不幸没有WIFI,过了8天没有1984的生活)，看电影，看书（看完了徐中约下册），帮老头倒水（开始不想倒，看多了天朝，觉得万一老头喝水噎死了，会不会讹我，后来实在受不了他30秒一次的叫喊，最长持续两个钟）。我同房的老头们多是不能自理的，医院里面有专门的护工帮换尿布，帮助喂食（大陆要自己请貌似）。医生经常问我咳血吗，让我觉得咳血很严重，很雷的是一个老头我看他住院，两天后我看他出院，那早上医生查房的时候问“咳血吗。&quot; ”咳“。下午他出院了。。我当时想抓住医生说”我也咳!&quot; 还有很多搞笑的事情：隔离期间冒死进来和我扯淡的护士，她说20年前去过北京啥的，扯了半天。。当晚我烧的很高；还有看见我玩电脑的医生，问我怎么不包月无线上网啥的，后来和另一个医生说他没事就用那个上黄网。。。看咸（han）片。。我低头看看他身上唯一身份的象征（一双耐克滑板鞋），全明白了，毕竟是公立医院。<br />
出院了，最振奋人心的时候到了，平均公众病房服务费用23,120港元；应收800港元；政府津贴金额22,320港元。住了八天医院，指使了许多小护士，吃了很多肉，浪费了许多口罩面具杯具等，加上3次X光，数不清的静脉注射，无数次的量血压，心跳，各种morning call，电视连续剧，外加出院后一个星期的药量，以及老头。只要了我800文，港府又给我垫了那么多钱，想起天朝医院的乱收费，治死人，收红包等。我望向北方，目光穿过狮子山，内牛满面。。。</div>

							
							
							
							
