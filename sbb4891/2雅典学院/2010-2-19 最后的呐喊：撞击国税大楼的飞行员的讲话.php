<br />
还是发在这里吧，比较有人气。<br />
<br />
　　If you’re reading this, you’re no doubt asking yourself, “Why did this have to happen?” The simple truth is that it is complicated and has been coming for a long time. The writing process, started many months ago, was intended to be therapy in the face of the looming realization that there isn’t enough therapy in the world that can fix what is really broken. Needless to say, this rant could fill volumes with example after example if I would let it. I find the process of writing it frustrating, tedious, and probably pointless… especially given my gross inability to gracefully articulate my thoughts in light of the storm raging in my head. Exactly what is therapeutic about that I’m not sure, but desperate times call for desperate measures. We are all taught as children that without laws there would be no society, only anarchy.<br />
<br />
如果你读到了这封信，那么你无疑会问你自己：“为什么会发生这样的事情？”简单的事实是，原因很复杂，也持续了很长时间。这一写作过程，从好几个月前开始，原本是想要挽救这个尽管已经无可救药的世界。无需多言，如果我愿意，这篇战书可以充满一个又一个的例子。我发现这封信的写作过程十分的令我沮丧，无聊，很可能还毫无意义……尤其是，我的脑袋里充满了暴怒的时候，要精准的表达我的想法就很困难了。究竟如何挽救，我并不清楚，但是绝望的时候需要绝望的举措。我们小时候都被教育，没有法律就没有社会，只有混乱。<br />
<br />
　　Sadly, starting at early ages we in this country have been brainwashed to believe that, in return for our dedication and service, our government stands for justice for all. We are further brainwashed to believe that there is freedom in this place, and that we should be ready to lay our lives down for the noble principals represented by its founding fathers. Remember? One of these was “no taxation without representation”. I have spent the total years of my adulthood unlearning that crap from only a few years of my childhood. These days anyone who really stands up for that principal is promptly labeled a “crackpot”, traitor and worse. While very few working people would say they haven’t had their fair share of taxes (as can I), in my lifetime I can say with a great degree of certainty that there has never been a politician cast a vote on any matter with the likes of me or my interests in mind. Nor, for that matter, are they the least bit interested in me or anything I have to say.<br />
<br />
很遗憾，从幼年起，我们在这个国家内就被洗脑，相信我们的政府代表正义，作为对我们的服务和奉献的回报。我们还被洗脑相信这片土地上有自由，并且为了我们的建国大爷们所代表的崇高原则，我们要随时准备献出生命。还记得吗？这些原则中有一条是“没有代表权就不得征税”。我所有成年后的年月里，一直都试图忘记小时候那少数几年被灌输的垃圾。这些日子来，只要有谁真正坚持这些原则，就会被迅速贴上疯子、叛徒或者更难听的标签。尽管，有工作的人不太可能说他们缴的税不合理（我就能这么说），但在我的一生中，我很确定从来没有一个政治家为我，以及和我一样的人，所关心的事情投过票。他们也对我以及我必须要说的话丝毫没有兴趣。<br />
<br />
　　Why is it that a handful of thugs and plunderers can commit unthinkable atrocities (and in the case of the GM executives, for scores of years) and when it’s time for their gravy train to crash under the weight of their gluttony and overwhelming stupidity, the force of the full federal government has no difficulty coming to their aid within days if not hours? Yet at the same time, the joke we call the American medical system, including the drug and insurance companies, are murdering tens of thousands of people a year and stealing from the corpses and victims they cripple, and this country’s leaders don’t see this as important as bailing out a few of their vile, rich cronies. Yet, the political “representatives” (thieves, liars, and self-serving scumbags is far more accurate) have endless time to sit around for year after year and debate the state of the “terrible health care problem”. It’s clear they see no crisis as long as the dead people don’t get in the way of their corporate profits rolling in. And justice? You’ve got to be kidding!<br />
<br />
为什么，有一大把强盗能够犯下如此难以想象的暴行（通用汽车的执行层的案例里，这种暴行还持续了好多年），而当他们的暴富即将在贪婪和不可救药的愚蠢的重压下被挤垮的时候，整个联邦政府的力量在几天之内（如果不是几个小时），都被轻而易举用来全力帮助这群强盗？而就在同时，我们称之为笑话的美国医疗系统，包括医药和保险公司，正在每年谋杀数以万计的人，从这些尸体和其他被他们玩残的受害者身上弄钱，而这个国家的领导者并不觉得这是一件很严重的事情，还不如保释出一些恶劣的暴发户同党来的重要。而且，议员们（称他们为小偷，诈骗犯，和自私自利的无耻小人更为准确）却有无穷的时间一年又一年坐而论道，辩论国家的“糟糕的卫生保健问题”。很明显，只要死人不会活过来阻挡他们的财路，在他们眼里这就不是什么危机。公正？你开什么玩笑呢！<br />
<br />
　　How can any rational individual explain that white elephant conundrum in the middle of our tax system and, indeed, our entire legal system? Here we have a system that is, by far, too complicated for the brightest of the master scholars to understand. Yet, it mercilessly “holds accountable” its victims, claiming that they’re responsible for fully complying with laws not even the experts understand. The law “requires” a signature on the bottom of a tax filing; yet no one can say truthfully that they understand what they are signing; if that’s not “duress” than what is. If this is not the measure of a totalitarian regime, nothing is.<br />
<br />
任何有理智的人都无法解释我们的税收和法律体制中那个累赘低效的巨大难题。到目前为止，我们的体制太过复杂，就是最聪明的学术大师也搞不清楚。而同时，它却无情地让受害者“承担责任”，声称他们有责任完全服从法律的要求，而即使是专家也无法理解这些法律。法律“要求”报税文件的底部必须要有报税者的签名，而没有人敢说他们完全理解自己签名的文件。如果这不是“强迫”，那这是什么？如果这还不算极权主义统治手段，那么没有什么能算。<br />
<br />
　　How did I get here?<br />
我是怎么走到这一步的呢？<br />
<br />
　　My introduction to the real American nightmare starts back in the early ‘80s. Unfortunately after more than 16 years of school, somewhere along the line I picked up the absurd, pompous notion that I could read and understand plain English. Some friends introduced me to a group of people who were having ‘tax code’ readings and discussions. In particular, zeroed in on a section relating to the wonderful “exemptions” that make institutions like the vulgar, corrupt Catholic Church so incredibly wealthy. We carefully studied the law (with the help of some of the “best”, high-paid, experienced tax lawyers in the business), and then began to do exactly what the “big boys” were doing (except that we weren’t steeling from our congregation or lying to the government about our massive profits in the name of God). We took a great deal of care to make it all visible, following all of the rules, exactly the way the law said it was to be done.<br />
<br />
我对这个真实的美国式噩梦的介绍，要从80年代早期开始。不幸的是，读了16年书之后，我不知从哪儿学到了一种荒唐而又自大的想法，自以为能够阅读和理解直白易懂的英语。一些朋友介绍我加入一个讨论“税收代码”的组群。这群人主要是在集中讨论一个美妙的关于“免税”的条款，这一条款使得诸如腐败粗鄙的天主教教堂之类的机构能够富得流油。我们仔细的研究法律（得到了一些“最好的”、高收入、有经验的商业税收律师的帮助），开始照着样子做那些“大人物”们所做的事情（除了我们从来没有以上帝的名义从信徒那儿骗钱，或者对政府撒谎隐瞒我们巨额的利润）。我们费了很大的劲儿使我们的业务透明，遵照所有的规定，完全按照法律的要求来做。<br />
<br />
　　The intent of this exercise and our efforts was to bring about a much-needed reevaluation of the laws that allow the monsters of organized religion to make such a mockery of people who earn an honest living. However, this is where I learned that there are two “interpretations” for every law; one for the very rich, and one for the rest of us… Oh, and the monsters are the very ones making and enforcing the laws; the inquisition is still alive and well today in this country.<br />
<br />
我们这么做的目的是为了促进对现行法律的重新评估，这些法律使得诚实挣钱的人同怪物般的宗教组织比起来，简直就是个笑柄。然而，这时我才学到一件事情：每本法律都有两种解释，一种是富人的解释，另一种是我们其他所有人的解释……哦对了，这些怪物们正是制订和执行法律的人；在这个国家里，宗教法庭直到今日仍然健在。<br />
<br />
　　That little lesson in patriotism cost me $40,000+, 10 years of my life, and set my retirement plans back to 0. It made me realize for the first time that I live in a country with an ideology that is based on a total and complete lie. It also made me realize, not only how naive I had been, but also the incredible stupidity of the American public; that they buy, hook, line, and sinker, the crap about their “freedom”… and that they continue to do so with eyes closed in the face of overwhelming evidence and all that keeps happening in front of them.<br />
<br />
这点小小的爱国主义教训花费了我四万美金和十年的生命，还把我的养老金计划清零。这使我意识到，我生活在一个意识形态完全构建在谎言上的国家。这还使我意识到，不仅我自己曾经那么幼稚，美国的公众也是不可救药的愚蠢；他们完完全全的相信了所谓的“自由”……他们即使面对压倒性的证据，也选择性失明。<br />
<br />
　　Before even having to make a shaky recovery from the sting of the first lesson on what justice really means in this country (around 1984 after making my way through engineering school and still another five years of “paying my dues”), I felt I finally had to take a chance of launching my dream of becoming an independent engineer. On the subjects of engineers and dreams of independence, I should digress somewhat to say that I’m sure that I inherited the fascination for creative problem solving from my father. I realized this at a very young age.<br />
<br />
甚至，在我从一场有关“正义在这个国家到底意味着什么”的惨痛教训中缓慢恢复过来之前（那是在1984年，我终于从工程学校毕业，但仍然花了5年付清贷款），我觉得我终于可以开始实现成为独立工程师的梦想了。说到工程和独立梦想的话题，我应该说句题外话：我从我父亲那里继承了创造性地解决问题的魔力。我很小的时候就意识到了这一点。<br />
<br />
　　The significance of independence, however, came much later during my early years of college; at the age of 18 or 19 when I was living on my own as student in an apartment in Harrisburg, Pennsylvania. My neighbor was an elderly retired woman (80+ seemed ancient to me at that age) who was the widowed wife of a retired steel worker. Her husband had worked all his life in the steel mills of central Pennsylvania with promises from big business and the union that, for his 30 years of service, he would have a pension and medical care to look forward to in his retirement. Instead he was one of the thousands who got nothing because the incompetent mill management and corrupt union (not to mention the government) raided their pension funds and stole their retirement. All she had was social security to live on.<br />
<br />
而关于独立的意义，直到我大学的前几年才认识到。18还是19岁的时候，我在宾夕法尼亚州的哈里斯堡作为学生独自生活。我的邻居是一位退休寡妇（八十多岁对我那个年纪来说是很古老的），她的丈夫以前是个钢铁工人。她丈夫的一声都在宾夕法尼亚中心的钢铁厂工作，大老板们和工会保证他，工作三十年后，他可以得到退休金和医疗关照。结果是，他和其他几千人一样什么也没得到，因为无能的工厂管理层和腐败的工会（更不用提政府了）劫掠了他们所有的退休金。她现在只能依靠社会保险来过活。<br />
<br />
　　In retrospect, the situation was laughable because here I was living on peanut butter and bread (or Ritz crackers when I could afford to splurge) for months at a time. When I got to know this poor figure and heard her story I felt worse for her plight When I got to know this poor figure and heard her story I felt worse for her plight than for my own (I, after all, I thought I had everything to in front of me). I was genuinely appalled at one point, as we exchanged stories and commiserated with each other over our situations, when she in her grandmotherly fashion tried to convince me that I would be “healthier” eating cat food (like her) rather than trying to get all my substance from peanut butter and bread. I couldn’t quite go there, but the impression was made. I decided that I didn’t trust big business to take care of me, and that I would take responsibility for my own future and myself.<br />
<br />
回顾当时的情况，更有讽刺意味的是，我却一连几个月吃花生酱和面包（当我有闲钱可以挥霍的时候，还吃麦片乐之薄饼）。当我听说了这个可怜的寡妇和她的故事后，我觉得她比我更杯具（我当时毕竟还觉得自己拥有一切）。我们互相交流，惺惺相惜，而当她像祖母一般劝说我，不要光吃花生酱和面包，而应该（像她一样）吃猫粮才能更健康的时候，我当时就震惊了。我并没有像她那样，但是这件事情给我造成了影响。我决定我决不能相信大公司会照顾我，我必须为自己的将来承担责任。<br />
<br />
　　Return to the early ‘80s, and here I was off to a terrifying start as a ‘wet-behind-theears’ contract software engineer… and two years later, thanks to the fine backroom, midnight effort by the sleazy executives of Arthur Andersen (the very same folks who later brought us Enron and other such calamities) and an equally sleazy New York Senator (Patrick Moynihan), we saw the passage of 1986 tax reform act with its section 1706.<br />
<br />
回到八十年代早期，我还是个乳臭未干的合约软件工程师，即将开始一段糟糕的经历……两年后，多亏了卑鄙的安达信高管（就是后来带来安然丑闻的那群人）以及一位同样卑鄙的的纽约州参议员（帕特里克 莫伊尼汉）的夤夜密谋，我们看到了1986年税务改革法案的1706条款。<br />
<br />
　　For you who are unfamiliar, here is the core text of the IRS Section 1706, defining the treatment of workers (such as contract engineers) for tax purposes. Visit this link for a conference committee report (<a href="http://www.synergistech.com/1706.shtml#ConferenceCommitteeReport" target="_blank">http://www.synergistech.com/1706.shtml#ConferenceCommitteeReport</a>) regarding the intended interpretation of Section 1706 and the relevant parts of Section 530, as amended. For information on how these laws affect technical services workers and their clients, read our discussion here (<a href="http://www.synergistech.com/ic-taxlaw.shtml" target="_blank">http://www.synergistech.com/ic-taxlaw.shtml</a>).<br />
<br />
如果你对此不熟悉的话，这里是1706税务条款的核心文本，它定义了工人（例如合约工程师）的税收待遇。访问这个链接（<a href="http://www.synergistech.com/1706.shtml#ConferenceCommitteeReport" target="_blank">http://www.synergistech.com/1706.shtml#ConferenceCommitteeReport</a>）可以看到一份委员会报告，关于1706条款以及相关的530修正条款的意图。有关这些法律如何影响技术服务工人以及他们的客户，可以参见这里的讨论（<a href="http://www.synergistech.com/ic-taxlaw.shtml" target="_blank">http://www.synergistech.com/ic-taxlaw.shtml</a>）。<br />
<br />
　　SEC. 1706. TREATMENT OF CERTAIN TECHNICAL PERSONNEL. (a) IN GENERAL C Section 530 of the Revenue Act of 1978 is amended by adding at the end thereof the following new subsection: (d) EXCEPTION. C This section shall not apply in the case of an individual who pursuant to an arrangement between the taxpayer and another person, provides services for such other person as an engineer, designer, drafter, computer programmer, systems analyst, or other similarly skilled worker engaged in a similar line of work. (b) EFFECTIVE DATE. C The amendment made by this section shall apply to remuneration paid and services rendered after December 31, 1986.<br />
<br />
1706条。特定技术人员的税收待遇。(a)对1978年税收法案的530条款增加如下修正：(d)例外――这一条款不适用于根据纳税人和第三方协议，为第三方提供诸如工程师，设计师，制图师，计算机程序员，系统分析师，或者其他相似的技术工作的个人。(b)生效日期-此增补条款对1986年12月31日后支付的工资和提供的服务生效。<br />
<br />
　　Note:<br />
<br />
　　! “another person” is the client in the traditional job-shop relationship.<br />
<br />
　　! “taxpayer” is the recruiter, broker, agency, or job shop.<br />
<br />
　　! “individual”, “employee”, or “worker” is you.<br />
<br />
注释：<br />
&nbsp;&nbsp;！“第三方”是指传统的雇员-雇主关系中的客户。<br />
&nbsp;&nbsp;！“纳税人”是指雇主或者代理人。<br />
&nbsp;&nbsp;！“个人”，“雇员”或者“工人”就是你。<br />
<br />
　　Admittedly, you need to read the treatment to understand what it is saying but it’s not very complicated. The bottom line is that they may as well have put my name right in the text of section (d). Moreover, they could only have been more blunt if they would have came out and directly declared me a criminal and non-citizen slave. Twenty years later, I still can’t believe my eyes.<br />
<br />
　　During 1987, I spent close to $5000 of my ‘pocket change’, and at least 1000 hours of my time writing, printing, and mailing to any senator, congressman, governor, or slug that might listen; none did, and they universally treated me as if I was wasting their time. I spent countless hours on the L.A. freeways driving to meetings and any and all of the disorganized professional groups who were attempting to mount a campaign against this atrocity. This, only to discover that our efforts were being easily derailed by a few moles from the brokers who were just beginning to enjoy the windfall from the new declaration of their “freedom”. Oh, and don’t forget, for all of the time I was spending on this, I was loosing income that I couldn’t bill clients. After months of struggling it had clearly gotten to be a futile exercise. The best we could get for all of our trouble is a pronouncement from an IRS mouthpiece that they weren’t going to enforce that provision (read harass engineers and scientists). This immediately proved to be a lie, and the mere existence of the regulation began to have its impact on my bottom line; this, of course, was the intended effect. Again, rewind my retirement plans back to 0 and shift them into idle.<br />
<br />
　　If I had any sense, I clearly should have left abandoned engineering and never looked back. Instead I got busy working 100-hour workweeks. Then came the L.A. depression of the early 1990s. Our leaders decided that they didn’t need the all of those extra Air Force bases they had in Southern California, so they were closed; just like that. The result was economic devastation in the region that rivaled the widely publicized Texas S&amp;L fiasco. However, because the government caused it, no one gave a shit about all of the young families who lost their homes or street after street of boarded up houses abandoned to the wealthy loan companies who received government funds to “shore up” their windfall. Again, I lost my retirement.<br />
<br />
　　Years later, after weathering a divorce and the constant struggle trying to build some momentum with my business, I find myself once again beginning to finally pick up some speed. Then came the .COM bust and the 911 nightmare. Our leaders decided that all aircraft were grounded for what seemed like an eternity; and long after that, ‘special’ facilities like San Francisco were on security alert for months. This made access to my customers prohibitively expensive. Ironically, after what they had done the Government came to the aid of the airlines with billions of our tax dollars … as usual they left me to rot and die while they bailed out their rich, incompetent cronies WITH MY MONEY! After these events, there went my business but not quite yet all of my retirement and savings.<br />
<br />
　　By this time, I’m thinking that it might be good for a change. Bye to California, I’ll try Austin for a while. So I moved, only to find out that this is a place with a highly inflated sense of self-importance and where damn little real engineering work is done. I’ve never experienced such a hard time finding work. The rates are 1/3 of what I was earning before the crash, because pay rates here are fixed by the three or four large companies in the area who are in collusion to drive down prices and wages… and this happens because the justice department is all on the take and doesn’t give a {EXPLETIVE DELETED}&nbsp;&nbsp;about serving anyone or anything but themselves and their rich buddies.<br />
<br />
　　To survive, I was forced to cannibalize my savings and retirement, the last of which was a small IRA. This came in a year with mammoth expenses and not a single dollar of income. I filed no return that year thinking that because I didn’t have any income there was no need. The sleazy government decided that they disagreed. But they didn’t notify me in time for me to launch a legal objection so when I attempted to get a protest filed with the court I was told I was no longer entitled to due process because the time to file ran out. Bend over for another $10,000 helping of justice.<br />
<br />
　　So now we come to the present. After my experience with the CPA world, following the business crash I swore that I’d never enter another accountant’s office again. But here I am with a new marriage and a boatload of undocumented income, not to mention an expensive new business asset, a piano, which I had no idea how to handle. After considerable thought I decided that it would be irresponsible NOT to get professional help; a very big mistake.<br />
<br />
　　When we received the forms back I was very optimistic that they were in order. I had taken all of the years information to Bill Ross, and he came back with results very similar to what I was expecting. Except that he had neglected to include the contents of Sheryl’s unreported income; $12,700 worth of it. To make matters worse, Ross knew all along this was missing and I didn’t have a clue until he pointed it out in the middle of the audit. By that time it had become brutally evident that he was representing himself and not me.<br />
<br />
　　This left me stuck in the middle of this disaster trying to defend transactions that have no relationship to anything tax-related (at least the tax-related transactions were poorly documented). Things I never knew anything about and things my wife had no clue would ever matter to anyone. The end result is… well, just look around. I remember reading about the stock market crash before the “great” depression and how there were wealthy bankers and businessmen jumping out of windows when they realized they screwed up and lost everything. Isn’t it ironic how far we’ve come in 60 years in this country that they now know how to fix that little economic problem; they just steal from the middle class (who doesn’t have any say in it, elections are a joke) to cover their asses and it’s “business-as-usual”. Now when the wealthy {EXPLETIVE DELETED} up, the poor get to die for the mistakes… isn’t that a clever, tidy solution. As government agencies go, the FAA is often justifiably referred to as a tombstone agency, though they are hardly alone. The recent presidential puppet GW Bush and his cronies in their eight years certainly reinforced for all of us that this criticism rings equally true for all of the government. Nothing changes unless there is a body count (unless it is in the interest of the wealthy sows at the government trough). In a government full of hypocrites from top to bottom, life is as cheap as their lies and their self-serving laws.<br />
<br />
　　I know I’m hardly the first one to decide I have had all I can stand. It has always been a myth that people have stopped dying for their freedom in this country, and it isn’t limited to the blacks, and poor immigrants. I know there have been countless before me and there are sure to be as many after. But I also know that by not adding my body to the count, I insure nothing will change. I choose to not keep looking over my shoulder at “big brother” while he strips my carcass, I choose not to ignore what is going on all around me, I choose not to pretend that business as usual won’t continue; I have just had enough.<br />
<br />
　　I can only hope that the numbers quickly get too big to be white washed and ignored that the American zombies wake up and revolt; it will take nothing less. I would only hope that by striking a nerve that stimulates the inevitable double standard, knee-jerk government reaction that results in more stupid draconian restrictions people wake government reaction that results in more stupid draconian restrictions people wake up and begin to see the pompous political thugs and their mindless minions for what they are. Sadly, though I spent my entire life trying to believe it wasn’t so, but violence not only is the answer, it is the only answer. The cruel joke is that the really big chunks of shit at the top have known this all along and have been laughing, at and using this awareness against, fools like me all along.<br />
<br />
　　I saw it written once that the definition of insanity is repeating the same process over and over and expecting the outcome to suddenly be different. I am finally ready to stop this insanity. Well, Mr. Big Brother IRS man, let’s try something different; take my pound of flesh and sleep well.<br />
<br />
<br />
　　The communist creed: From each according to his ability, to each according to his need.<br />
共产主义信条：各尽所能，各取所需。<br />
<br />
　　The capitalist creed: From each according to his gullibility, to each according to his greed.<br />
资本主义信条：人尽其蠢，我尽其贪。<br />
<br />
　　Joe Stack (1956-2010)<br />
