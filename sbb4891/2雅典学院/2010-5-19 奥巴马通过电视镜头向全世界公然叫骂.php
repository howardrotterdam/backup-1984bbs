													<h2>何亚福:中国人不是废物！</h2>
						
						
																											<div id="postmessage_626098" class="t_msgfont">何亚福:中国人不是废物！<br />
<br />
<br />
----关于人口与计划生育的对话<br />
<br />
何亚福<br />
<br />
甲：实行计划生育以来，中国少生了3亿人，节省了7万亿元抚养费。平均来说，少生一个人节省了2万多元的抚养费。<br />
<br />
乙：按照这个逻辑，如果你的父母对你说：“当初如果不生你，我们就节省2万多元的抚养费了。”你会有什么感觉？难道少生的那3亿人都是废物？他们长大之后不会创造出大于他们抚养费的价值？抚养孩子的收益大于花费，换句话说，人的价值是正数而不是负数，这是一个常识，如果你还没有这个常识，说明你认为人类社会不是进步而是倒退的。<br />
<br />
甲：我看到新闻报道说，中国少生了3亿人，为世界人民作出了贡献。<br />
<br />
乙：除了中华民族以外，世界上没有哪一个民族认为减少本民族的人口是对世界人民作贡献的！如果认为中国人是废物，那么减少中国人口当然是为世界作贡献，因为世界上减少了废物；如果认为中国人是有价值的，那么减少中国人口就是世界的损失了！中国的新闻媒体宣传说，中国少生了3亿人是为世界人民作出了贡献。然而，世界人民并不会为此而感激中国人，只有那些一直嫌中国人多的种族主义者，才会为大量减少中国人口而喝彩!<br />
<br />
甲：你谈到“种族主义者”，英国曼联前主帅阿特金森说过：“我难以理解，为什么中国会有人口问题？他们本来有最好的避孕方法----中国女人是世界上最丑的。” 这使我很气愤！<br />
<br />
乙：既然一个民族自己正在努力地使用行政手段减少本民族的人口，那么，还有谁不认为这个民族不应该减少？既然一个民族自己认为自己的存在是消耗世界资源，认为减少自己是对世界作出贡献，那么还有谁会不这样认为？既然一个民族自己认为本民族人口太多了，应该大量减少人口，那么还有谁不认为“他们本来有最好的避孕方法”？正应了这句话：“自尊者人必尊之，自贱者人必贱之。” <br />
<br />
甲：但是如果取消计划生育，谁来养活多生出来的人口？他们都到你家吃饭行不行?<br />
<br />
乙：多生出来的人口难道全部是残疾人? 他们都失去了劳动能力? 即使是残疾人，也并不一定全部都靠别人养活的。提出这个问题的人是在假定：人都是只会消费价值而不会创造价值的废物！我不知道提出这个问题的人本身是不是一个寄生虫！<br />
<br />
甲：我问你：一个国家养活一亿人容易还是养活十亿人容易? 当然是养活一亿人更容易吧!<br />
<br />
乙：你怎么不问：一个国家养活一百万人容易还是养活十亿人容易？究竟是国家养活人民还是人民养活国家？世界上有许多国家，人口只有几百万甚至几十万，但这些国家却很贫穷；世界上也有许多国家，人口有几千万甚至上亿，但这些国家却很富裕。提出这个问题的人与上面这位“寄生虫”有着同样的思维模式。<br />
<br />
甲：但是中国有13亿人口，生存压力太大了。<br />
<br />
乙：生存压力的大小，主要不取决于人口的多少。世界上人口密度大的国家，生活水平也并不一定比人口密度小的国家差。中国并不是生存压力最大的国家，人口密度比中国大、人均自然资源比中国少的国家有几十个，但他们都没有通过减少人口来解决生存压力问题。你如果嫌人多，那你愿意搬到荒无人烟的地方去住吗？企图通过减少人口来提高人均自然资源拥有量进而变得富裕起来，其实是一种极为短视的懒汉思想！ <br />
<br />
甲：计划生育是为了使中国人过得更好，牺牲的是我们这一代人，幸福的是后人。<br />
<br />
乙：与你设想的正好相反，计划生育可以让现在的这一代人过上相对好一点的日子（因为节省了抚养后代的费用），但是留给后代的是老龄化、性别比失衡、社会需求不足、国家竞争力下降等等恶果。如果真的为后代着想，就不要把一个老龄化社会的烂摊子留给后代！计划生育宣传说“为后代着想”，而实际上，拒绝承担养育责任、贪图现在的享受才是真正的不为后代着想！<br />
<br />
甲：现在有一些人说：“中国的各种问题，归根结底都是人口太多的问题。”<br />
<br />
乙：有些人把什么罪过都归咎于“人口太多”，人口多、人均收入低和人均资源少曾经为我国的国民经济和社会生活提供过太多的借口，当了太长时间的替罪羊，但是资源并不是单一的矿产、土地等等，人口本身就是一种资源，没了人你能干成什么事情？事实上，中国成为世界制造业中心，不是靠技术、靠资金而是靠廉价劳动人口支撑起来的，对于当代中国来说，人口多是谁也无法否认的优势。中国的教育经费占GDP的比例不及非洲穷国乌干达，这样，一方面由于国家政策的原因，使大量的人力资源白白变成了垃圾；另一方面政丨府又反过来告诉我们，中国的一切问题，就是你们这些人太多造成的，所以中国必须大量减少人口才行！<br />
<br />
甲：这么说，所谓“人口太多”，其实是一种借口？<br />
<br />
乙：是的，所谓“人口太多”其实是政策失误、吏治腐败和制度缺陷的挡箭牌和遮羞布。我们要警惕的是，对体制改革和社会进步的无所作为，而对人口的歇斯底里。在一些人眼里，人口要为腐败作出牺牲，人口要为扭曲的体制作出牺牲，因为一切罪在人口。实际上，所谓的生存压力问题，哪怕你腐败少那么一点，体制理顺那么一点，都将极大减轻社会和个人的生存压力。人类社会的进步都是缘于改革体制与科技发展，而非源于自我阉割。只要有顺畅的体制与不断进步的科技，我们能交给子孙后代一个美丽的河山和一个富裕的社会，而无需剥夺他们的兄弟姐妹。</div>

							
							
							
							
