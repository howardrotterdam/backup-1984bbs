																											<div id="postmessage_625223" class="t_msgfont">Face to face with Obama<br />
<br />
Print<br />
Australian Broadcasting Corporation<br />
Broadcast: 14/04/2010<br />
Reporter: 7.30 Report<br />
Kerry O’Brien speaks exclusively to United States President Barack Obama at the White House in Washington. The President confirms plans to visit Australia in June with his family and offers insights about the war in Afghanistan, China’s emergence as an economic power and climate change. <br />
<br />
<br />
Transcript<br />
KERRY O’BRIEN: PRESIDENT OBAMA WELCOME TO AUSTRALIAN TELEVISION.<br />
<br />
PRESIDENT OBAMA:Thank you so much.<br />
<br />
KERRY O’BRIEN: YOU’VE HAD SOME UNDERSTANDABLE HICCUPS TRYING TO GET TO OUR PART OF THE WORLD. <br />
<br />
PRESIDENT OBAMA: Right.<br />
<br />
KERRY O’BRIEN: IS JUNE LOCKED IN FOR YOUR FIRST VISIT, AND HOW LONG WILL YOU BE ABLE TO STAY?<br />
<br />
PRESIDENT OBAMA: Well we are definitely looking at travelling in June and my hope is that we can stay at least a couple of days, I want to be able to take Michelle and hopefully the girls to Sydney as well as Canberra and...<br />
<br />
KERRY O’BRIEN: BECAUSE YOU’VE GOT MEMORIES?<br />
<br />
PRESIDENT OBAMA: I’ve got wonderful memories, I used to travel through Australia when my mother was living in Indonesia and my grandparents were living in Hawaii, and so we’d usually go through Sydney and memories I have not only of it being a beautiful country but of people being just wonderfully hospitable and kind to me are ones that I carry with me.<br />
<br />
KERRY O’BRIEN: WELL WE’D SEE THAT AS A GREAT INVESTMENT <br />
<br />
PRESIDENT OBAMA: Thank you.<br />
<br />
KERRY O’BRIEN: PRESIDENTS AND PRIME MINISTERS ALWAYS TALK ABOUT A SPECIAL US-AUSTRALIAN RELATIONSHIP AND ONE OF YOUR SENIOR STATE DEPARTMENT OFFICIALS TALKED ABOUT A MEETING OF MINDS <br />
<br />
PRESIDENT OBAMA:Yeah<br />
<br />
KERRY O’BRIEN: BETWEEN YOU AND KEVIN RUDD. IS THERE A MEETING OF MINDS?<br />
<br />
PRESIDENT OBAMA: I really do think there is, you know Kevin is somebody who I probably share as much of a world view as any world leader out there, I find him smart but humble, he works wonderfully well in multilateral settings, he’s always constructive, incisive. And, you know I think he is like me a pragmatic person, I think he comes to the job wanting to provide better opportunities not just for this generation but for the next but I think you know he’s somebody who isn’t an academic, or just thinking about abstract ideas, I think he’s constantly thinking in very practical terms about how to get something done.<br />
<br />
KERRY O’BRIEN:AFGHANISTAN WILL OBVIOUSLY BE ON YOUR AGENDA WHEN YOU TWO MEET. YOU ONCE FAMOUSLY SAID OF THE WAR IN IRAQ: “I’M NOT OPPOSED TO ALL WARS. I’M OPPOSED TO DUMB WARS.” <br />
<br />
PRESIDENT OBAMA: Right.<br />
<br />
KERRY O’BRIEN:IS AFGHANISTAN NOW AT RISK OF BECOMING A DUMB WAR?<br />
<br />
PRESIDENT OBAMA: No I think Afghanistan is a necessary mission. It’s a difficult one but it’s a necessary one. Obviously the reason we are there is very clearly defined, Al Qaeda used it as a launching point for an attack that killed 3000 Americans and persons of all nationalities and ethnicities. We have to root out the terrorist networks that are able to mount not just the sorts of attacks that we saw on 9/11 but also the sorts of attacks we saw on Bali that had such a devastating attack on Australian nationals. Australia is the largest non-NATO contributor to our efforts there, we are grateful for the sacrifice and Prime Minister Rudd and I are in constant consultation about how we move that effort forward in a way that stabilises Afghanistan, that trains up Afghan security forces so that we are able to remove our security forces but we have confidence that Afghan will not return to a launching point for Al Qaeda operations<br />
<br />
KERRY O’BRIEN: SO YOUR OBJECTIVES ARE CLEAR<br />
<br />
PRESIDENT OBAMA: Absolutely<br />
<br />
KERRY O’BRIEN: WHICH IS WHAT COLIN POWELL SAID AFTER THE EVENT ABOUT VIETNAM. THE LESSON ABOUT VIETNAM WAS HAVE CLEAR OBJECTIVES AND KNOW THAT YOU CAN WIN, SO THE FIRST OF THAT IS CORRECT, BUT WHEN YOU STARTED CAMPAIGNING FOR THE PRESIDENCY, YOU TALKED OF THE WAR IN AFGHANISTAN BEING IN ITS SEVENTH YEAR. IT’S NOW IN ITS NINTH YEAR, AMERICA’S IN DEEPER, AND THE SITUATION DOESN’T APPEAR TO BE GETTING ANY BETTER. YOU’RE NOT HEARING THE ECHOES OF VIETNAM IN THAT ARE YOU? <br />
<br />
PRESIDENT OBAMA: Well, I would dispute the notion that it’s not getting better. I do think that what we’ve seen is a blunting of the momentum of the Taliban which had been building up in the year prior to me taking office. I think that you’ve seen actually some reforms in the Karzai government that allow us to then actually get civilian help to ordinary Afghans and that then builds trust in the government, so I think we’ve got some positive trends but there is no doubt that this is a difficult task, and what I have said is that we need to begin drawing down our troops in 2011, and start handing over more and more responsibility to the Afghans, we can’t be there in perpetuity neither the American people nor the Australian people should be asked to carry that burden any longer than it needs to be carried.<br />
<br />
KERRY O’BRIEN:HOW DID YOU REACT WHEN PRESIDENT KARZAI’S RECENTLY REPORTED COMMENTS WERE FIRST CONVEYED TO YOU, THAT IF HE WAS PUT UNDER MORE PRESSURE, HE MIGHT JOIN THE TALIBAN?<br />
<br />
PRESIDENT OBAMA: Well you know I don’t take news reports of comments necessarily that seriously, I wait until I hear directly from a world leader, I know I’m misquoted a lot and you know what I do think is he’s a proud man, I think he has a strong sense of Afghan nationalism, and he’s under a very difficult circumstance. Look, this is a country that has gone through three decades of extraordinary turmoil, he is trying to rebuild a sense of a national government when that is not a tradition that’s been there for a long time and so he’s under enormous pressure, but what we have said is that we can’t succeed unless President Karzai moves forward on the reforms that are so necessary for Afghans to see a real investment in their lives day to day and improvement in their lives day to day<br />
<br />
KERRY O’BRIEN:BUT OF COURSE, HE ALSO SAID JUST BEFORE THAT ATTRIBUTED COMMENT THAT FOREIGNERS WERE BEHIND THE FRAUD, BEHIND THE FRAUD IN LAST YEAR’S ELECTION, WHICH EVEN YOUR OWN PRESS SECRETARY HAS DESCRIBED AS UNTRUTHFUL, AS HE ALSO DESCRIBED THOSE OTHER COMMENTS AS TROUBLING. IS PRESIDENT KARZAI STABLE ENOUGH AND WORTHY ENOUGH TO DESERVE YOUR BACKING AT A COST OF AMERICAN AND COALITION LIVES?<br />
<br />
PRESIDENT OBAMA: Well I think that President Karzai is capable of leading his country into the 21st Century and stabilising it. As I said he’s got a tough task but the reason we’re there ultimately is not to support one man. The reason we’re there is because it’s necessary for our national security, we think it’s necessary for the world’s security that we dismantle Al Qaeda and its affiliate networks, and we need a strong partner in that process, I think Karzai has the capacity to be that strong partner and I think that we’re going to have to keep on both respecting the sovereignty of Afghanistan and Karzai as the duly elected leader but continue to put pressure on him to make the kinds of reforms and improvements that will not only mean success for us but also ultimately success for him.<br />
<br />
KERRY O’BRIEN: LAST QUESTION ON AFGHANISTAN. IT WAS REPORTED IN AUSTRALIA IN MARCH THAT GENERAL McCHRYSTAL IN A PHONE CONVERSATION WITH AUSTRALIA’S DEFENCE FORCE CHIEF, QUOTE: “WARNED THAT THE RUDD GOVERNMENT’S REFUSAL TO ALLOW AUSTRALIAN TROOPS TO TAKE THE FIGHT TO THE TALIBAN WAS IMPAIRING THE WAR EFFORT”. WERE YOU AWARE OF THE GENERAL’S FRUSTRATION AND WOULD YOU LIKE TO SEE AUSTRALIA DOING MORE THAN IT IS?<br />
<br />
PRESIDENT OBAMA: You know I won’t comment on the reports of this particular conversation it’s not one that came to me. I think that Australia has made enormous sacrifices and an enormous investment both on the military side and on the civilian side, they’ve been a very strong partner and America is grateful for that.<br />
<br />
KERRY O’BRIEN: YOU’VE DESCRIBED NUCLEAR TERRORISM AS AMERICA’S GREATEST RISK IN THE SHORT AND LONG TERM, BUT CLIMATE CHANGE HAS ALSO BEEN DESCRIBED AS THE GREATEST SINGLE RISK TO HUMANITY. YOU MET CHINESE PRESIDENT HU ON MONDAY. DID YOU TWO MAKE ANY PROGRESS ON CLIMATE CHANGE, BECAUSE SURELY THE SOLUTION IS LARGELY IN THE END IN YOUR HANDS AND HIS? <br />
<br />
PRESIDENT OBAMA: Well I would dispute that any two countries can dictate a solution here, we’re going to have to have an investment by all nations in dealing with what I think is a very real problem. Australia has a significant carbon footprint just like we do, and certainly per capita, our two countries have some of the biggest carbon footprints. So we’re going to have to make an effort, I think the Copenhagen accord was progress and by the way Kevin Rudd’s participation in that was critical in helping us move the ball down the field. What we have for the first time is all countries documenting the mitigation efforts that they’re willing to make and that’s extremely significant. We’re going to have to do more though, and I know that both in the United States and in Australia, there is a concern that somehow there is a conflict between economic growth on the one hand and dealing aggressively with climate change. What I’ve been trying to say here in the United States and I would say to the world is that if we focus our attention, our ingenuity, our innovative capacity on transforming from a fossil fuel based economy to a clean energy based economy then potentially we can not only solve the problem of climate change but unleash an enormous amount of economic growth for the future but it’s going to take some time, and there’s going to be some transition and people are understandably resistant. It is always difficult to make big changes like the ones required for climate change in the midst of a very bad economic crisis and we’ve had the worst one since the 1930s. So, I’m confident though that you know ultimately humanity has a way of responding not always as timely as, on as timely basis as we would like, but when we start facing down potential catastrophe, humanity adapts and I think we will this time as well.<br />
<br />
KERRY O’BRIEN: DO YOU FEEL THAT YOU ARE MAKING HEADWAY WITH PRESIDENT HU ON THIS FRONT?<br />
<br />
PRESIDENT OBAMA: Well you know I think China has an enormous interest in solving this problem. You know if you talk to Chinese leaders I think they will acknowledge immediately that if over a billion Chinese citizens have the same living patterns as Australians and Americans do right now then all of us are in for a very miserable time, the planet just can’t sustain it, so they understand that they’ve got to make a decision about a new model that is more sustainable that allows them to pursue the economic growth that they’re pursuing while at the same time dealing with these environmental consequences. So I think they understand intellectually. Right now though they’re understandable impulse is to say well let’s let the developed countries, the Australias, and the Americas deal with this problem first and we’ll get to it when we’ve caught up a little bit in terms of our standard of living. The point we’ve tried to make is we can’t, we can’t allow China to wait. We have to take responsibility and do what needs to be done, but if emerging countries not just China but also India, Brazil and others are pursuing a path in which they replace us as the largest carbon emitters, that’s not a sustainable practical approach, so we’re going to have to have everybody moving on the same track at the same time.<br />
<br />
KERRY O’BRIEN: ARE AS YOU AS WEDDED TO, ARE YOU AS CONVINCED THAT A CARBON TRADING SCHEME IS THE BEST WAY TO GO, THE BEST WAY TO GET EMISSIONS DOWN AS KEVIN RUDD IS?<br />
<br />
PRESIDENT OBAMA: I think that, I am absolutely convinced you have to put a price on carbon of some sort, so there are a number of ways of doing that, you could do a carbon tax, you can do a cap and trade system which is what originally I had suggested, very similar to the program that Kevin Rudd has proposed, but the key is to change incentives for the market place, where we are actually pricing these pollutants that are going into our atmosphere, if a price is placed on them, if industry has to take them into account then we can count on the market place responding effectively. If it’s free to pollute then you’re never going to have a, companies making all the myriad decisions that are required in order for the entire system to move in a better direction, and it’s never sufficient for a command and control system in which government is trying to chase down every single polluter. What you have to do is have a market mechanism to make it work more efficiently. <br />
<br />
