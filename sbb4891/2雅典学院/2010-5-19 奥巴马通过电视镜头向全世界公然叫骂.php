													<h2>何亚福:地球资源用完了怎么办?</h2>
						
						
																											<div id="postmessage_626096" class="t_msgfont">何亚福:地球资源用完了怎么办?<br />
<br />
何亚福<br />
<br />
甲：地球的自然资源是有限的，如果人口不断增长，自然资源总有用完的时候。所以必须实行计划生育来控制人口的增长!<br />
<br />
乙：是的, 自然资源是有限的, 人类只有一个地球, 目前, 人类也没有能力移民到其它星球. 不过, 如果你说实行计划生育的理由是“自然资源是有限的”, 那么这种理由是不能成立的! 这是因为: 第一, 即使中国实行计划生育, 也只能减少中国的人口, 其他国家是不会因为“自然资源是有限的”而减少本国人口的,他们是不会响应你“要减少大家一起来减少”的号召的。 第二, 即使全世界只剩下一亿人, 地球资源也总有用完的时候, 那时候怎么办? 总不成大家都自杀了吧?<br />
<br />
<br />
<br />
甲：那你说自然资源用完了之后怎么办?<br />
<br />
乙：一般来说, 面对生存压力, 不同的民族、不同的国家会作出不同的选择. 比方说, 有甲、乙两个国家, 他们的粮食生产都不足以养活本国人口. 于是, 甲国采取的措施是: 实行计划生育以减少本国人口; 乙国采取的措施是: 想方设法提高粮食产量, 并大力发展国民经济. 从这个例子中我们看到, 面对生存压力, 甲国选择了向内：企图改造自身来适应环境，结果只能走上退化的道路; 而乙国选择了向外：改造生存环境以适应自己的民族, 结果越来越富裕。<br />
<br />
<br />
<br />
甲：我知道粮食产量可以提高, 可是像石油这些能源属于不可再生资源, 地球上的石油只会越用越少, 而且没办法生产出来, 所以必须节约使用!<br />
<br />
乙：我再举甲、乙两个国家为例子: 当地球上的石油差不多用完的时候, 甲国通过一项法律, 禁止私人拥有小轿车, 以节省汽油; 乙国则千方百计研究能代替汽油的新能源, 结果研制出一种使用新能源的汽车, 并行销全世界。<br />
<br />
<br />
<br />
甲：你的意思是说，当地球上一种资源快用完的时候, 应该寻找另一种替代资源?<br />
<br />
乙：是的, 地球上任何一种资源都是有限的, 但是, 任何一种资源也都有其替代的资源. 所以, 我们根本不必担忧某种资源用完了怎么办? 而且, 究竟什么是资源？石油在地球上已经存在上百万年了，可真正大规模开发利用才100多年，200年前的人类会认为石油是资源吗？当然不会，所以说资源是科学技术进步的结果。当人不会使用煤的时候，煤只不过是黑石头；当人们不会使用汽油的时候，石油常年沉睡在地下。人类之所以数量越来越多, 人均自然资源越来越少, 生活反而越来越富裕, 就是不断战胜生存压力的结果, 而不是实行计划生育的结果。<br />
<br />
<br />
<br />
甲：但你怎么能肯定，当一种资源用完之后，人类一定能找到替代资源？如果人类找不到替代资源怎么办？所以，在百分之百地肯定人类能找到新的替代资源之后，才能放宽计划生育政策。<br />
<br />
乙：按照你的逻辑，你的母亲在生你之前，也要这样说：“我怎么能肯定，你长大之后一定能找到工作？如果你找不到工作怎么办？所以，在百分之百地肯定你能找到工作之后，才能把你生下来。”你的母亲不是先知，不可能在生你之前就知道你将来的职业具体是哪一种，但她知道，只要把你抚养大，并使你受到良好的教育，你自然能够找到工作。同样，我也不是先知，我不知道将来新的替代资源具体是哪一种，但我知道，人类总是在不断进步的。只要科学技术在进步，自然资源永远也不会枯竭。<br />
<br />
<br />
<br />
甲：现在中国的许多种人均自然资源都低于世界平均水平, 例如, 我国人均水资源只有2200立方米, 仅相当于世界人均水资源的1/4。所以只有把中国的人口降下来, 才能提高人均自然资源的拥有量。<br />
<br />
乙：为什么一定要提高人均自然资源的拥有量? 一个国家的人均自然资源丰富不等于这个国家富裕. 以人均水资源而言，以色列是600万人，它的土地60%是干旱和半干旱地区，人均水资源才270立方米。中国人均水资源是2200立方米，相当于以色列的8倍, 但中国的人均收入远远不及以色列。以色列作为世界上最缺水的国家，他并不实行计划生育以减少以色列的人口，相反，他发展出世界上最节水的灌溉技术。以色列人使用水已经上升到了一种水文明, 由漫灌到渠灌，由渠灌到中灌，由中灌到滴灌。以色列用水的文明已经达到了滴灌的境界。<br />
<br />
<br />
<br />
<br />
甲：但是中国西部的环境很恶劣, 不控制人口增长肯定是不行的.<br />
<br />
乙：环境是可以改变的。以色列建国之初, 其农业生产的条件也是十分恶劣的，发展农业生产的两大基本要素土地和水都不具备。而以色列人仅用短短的几十年时间，就在大部分是荒漠，水资源十分匮乏的土地上奇迹般地创造了一片片绿洲，无不令世人叹服。沙漠大棚温室、滴灌技术、沙漠菜田等奇迹的出现，都在说明和诠释着这样一个浅显的道理，即一切人类文明成果的出现都是人们辛勤努力和聪明智慧的结晶。如果以色列人也像某些中国人那样总是埋怨环境恶劣而不思进取, 以色列早就亡国了。<br />
<br />
<br />
<br />
<br />
甲：人类不能进行光合作用, 必须消耗其它动物或植物才能生存。一个人从出生到死亡, 都是消费者。<br />
<br />
乙：是的, 人类不能进行光合作用, 但其它动物也不能进行光合作用, 也必须消耗其它动物或植物才能生存. 你只看到人作为消费者的一面, 却看不到人作为创造者的一面. 一个人的一生中, 所创造的价值完全可以大于他(或她)所消费的价值. 也就是说, 人一生中所创造的价值减去消费的价值是正数而不是负数, 否则人类社会就不会越来越进步了。现在有些城市出台的这项规定是非常荒唐的: 不允许城市居民生第二胎, 却允许城市居民养宠物(例如大狼狗). 养一个宠物的花费往往比养一个小孩的花费更大, 而且宠物不能创造出有益于人类的价值。</div>

							
							
							
							
