什么是圣战【杰哈德】<br />
<br />
伊斯兰圣战是伊斯兰教的一个重要组成部分，是伊斯兰精神的最有力的体现和外化。在世界上的所有宗教中，圣战规模之大、持续之久、影响之深，莫过于伊斯兰教。从伊斯兰教诞生至今，它一直是一支“震撼世界的力量”，人类历史的回音壁上经久不息地回荡着伊斯兰圣战的呐喊。<br />
&nbsp; &nbsp;&nbsp; &nbsp;　　　　一<br />
&nbsp; &nbsp;&nbsp; &nbsp;一般认为，伊斯兰圣战分为两种，一种是为主道和信仰而战的小圣战，一种是同与生俱来的内在邪恶进行不懈斗争的大圣战。本文主要论及前者。<br />
&nbsp; &nbsp;&nbsp; &nbsp;圣战，是阿拉伯语“吉哈德”(JIHAD)的意译，即“奋斗”。从狭义上说，它原指先知穆罕默德与麦加多神教徒进行的战争，后来指在伊斯兰教旗帜下为宗教而进行的一切战争。(注：赫伯特・戈特沙尔克著：《震撼世界的伊斯兰教》，阎瑞松译，陕西人民出版社1987年版，第53页。)从广义上说，它是指传播伊斯兰教教义的一切活动，即为促进和保卫伊斯兰事业作出的任何努力，既包括说教的方式，也包括武力的方式。如巴基斯坦学者赛义德・阿布杜拉・毛杜迪在其所著的《伊斯兰生活方式》一书中所说：“吉哈德即尽自己最大努力，无论是通过嘴或笔或剑，传播安拉的话。”(注：王怀德、郭保华：《伊斯兰教史》，宁夏人民出版社1992年版，第206页。)<br />
&nbsp; &nbsp;&nbsp; &nbsp;伊斯兰圣战是伊斯兰教的重要组成部分，在伊斯兰教中占有重要的地位。伊斯兰教的两大重要经典《古兰经》和《圣训》都指出圣战的重要性，号召穆斯林为信仰而战。《圣训》将圣战视为伊斯兰教最重要的部分。《圣训》中曾记载穆罕默德的话说：“我岂能不把事情的尖顶、支柱和最重要部分告诉你们?事情的尖顶就是伊斯兰；支柱就是祈祷；它的最重要部分就是吉哈德。”(注：托马斯・李普曼著：《伊斯兰教与穆斯林世界》，陆文岳和英珊译，新华出版社1985年版，第144 页。)《古兰经》用大量篇幅论述圣战，规定“战争已成为你们的定制”(2：216)(注：马坚译：《古兰经》，中国社会科学出版社1981年版(冒号前后数字为章和节)。)；为主道而战的人将永远获胜，将得到真主的奖赏；为主道而战阵亡的信徒，比其余的信徒得天独厚，他们用不着等到世界末日，就可以马上进入乐园。乐园是一幅诱人的图景：“有水河，水质不腐；有乳河，乳味不变；有蜜河，蜜质纯洁。”(47：15)<br />
&nbsp; &nbsp;&nbsp; &nbsp;伊斯兰教经典对圣战的种种规定成为穆斯林投身于圣战的原动力。托马斯・李普曼就此评论道：“《古兰经》中作出的那些在同异教徒的战争中身亡的人进入乐园的许诺，是鼓励穆斯林采取政治和军事行动的一种力量。”(注：托马斯・李普曼著：《伊斯兰教与穆斯林世界》，陆文岳和英珊译，新华出版社1985年版，第 145页。)这一见解不无道理。<br />
&nbsp; &nbsp;&nbsp; &nbsp;伊斯兰圣战也有其教义方面的理论基础和渊源。根据传统的伊斯兰教义，整个世界被划分为两大部分：伊斯兰区域(DAR AL-ISLAM)和敌对区域(DAR AL-HARB)。前者指接受伊斯兰统治的地区，包括穆斯林居住的地区，以及接受穆斯林当局统治的非穆斯林地区；后者指伊斯兰区域之外，由异教徒统治的世界上其他所有地区。<br />
&nbsp; &nbsp;&nbsp; &nbsp;从理论上讲，伊斯兰的目标是把伊斯兰教传播到世界各地，让所有人都承认和信仰独一的安拉。因此，伊斯兰区域和非伊斯兰区域之间一直处于一种经常性的战争状态，伊斯兰教将这种战争称为“圣战”。在圣战间歇期间，允许穆斯林和异教徒通过共同边界，双方可以建立外交、贸易关系，但在非伊斯兰区域变为伊斯兰区域前，双方不可能有永久性和约。在伊斯兰教上升时期，一些穆斯林思想家提出了“伊斯兰国家与非伊斯兰国家不能永久共存”，“伊斯兰教的最终目的是将全世界都改变为伊斯兰领域”，“穆斯林必须不断通过‘吉哈德’使非伊斯兰地区转变为伊斯兰地区”等原则。(注：彭树智主编：《伊斯兰教与中东现代化进程》，西北大学出版社1997年版，第359页。)这些原则在很长一个历史时期内决定了伊斯兰世界与外部世界的关系，并使穆斯林不断向外扩张，用武力或非武力的方式向外传播伊斯兰教，实现了整个西亚北非的伊斯兰化。<br />
&nbsp; &nbsp;&nbsp; &nbsp;马克思曾就伊斯兰圣战的这一教义基础和渊源写道：“《古兰经》和以它为根据的伊斯兰教法律把各个不同民族的地理和人文归结为一个简便的公式，即把他们分为两种国家和民族――正统教徒和异教徒。异教徒就是‘哈尔比’，即敌人。伊斯兰教宣布异教徒是不受法律保护的，并在穆斯林和异教徒之间造成一种经常互相敌视的状态。”(注：《马克思恩格斯全集》第10卷，人民出版社1972年版，第180页。)这样的“人文学”从理论上论证并鼓励了具有神圣意义的征服战争。正如克雷维列夫所说，正是这种人文学思想将各个不同社会集团的群众组成一个有纪律的、志同道合者的阵营，在共同的宗教口号下进行远征。(注：约・阿・克雷维列夫著：《宗教史》(下卷)，乐峰等译，中国社会科学出版社1984年版，第147页。)<br />
&nbsp; &nbsp;&nbsp; &nbsp;伊斯兰教经典及教义对圣战所赋予的崇高地位及其相关规定，无疑是伊斯兰圣战不绝于史，伊斯兰信徒视死如归为主殉身的一个重要因素。<br />
&nbsp; &nbsp;&nbsp; &nbsp;　　　　二<br />
&nbsp; &nbsp;&nbsp; &nbsp;伊斯兰圣战是伊斯兰教的典型特征，为圣战效力是每个穆斯林的义务。那么，伊斯兰圣战的实质到底是什么?要探讨这个问题，就必须追溯伊斯兰圣战的缘起和形成，因为它的实质就隐含在其中。<br />
&nbsp; &nbsp;&nbsp; &nbsp;伊斯兰圣战首先起源于伊斯兰教诞生时期阿拉伯民族精神之中，它是阿拉伯民族性格的体现。正如王治心先生在《中国宗教思想史大纲》一书中所说：“一民族的宗教，正是其民族精神的表现：有强毅的阿拉伯民族，便会产生出富有团结和抵抗的回教。”(注：王治心：《中国宗教思想史大纲》，上海三联书店1988年版，第3页。)关于阿拉伯人的民族特性，众说纷纭，莫衷一是。笔者以为，阿拉伯的民族性格基调应在其赖以形成的自然环境和社会状况特征中去寻找。<br />
&nbsp; &nbsp;&nbsp; &nbsp;阿拉伯半岛是阿拉伯人的故乡，是伊斯兰教的摇篮。阿拉伯半岛自然条件的突出特征是沙漠广阔，水雨缺水，干燥炎热。这种特殊的自然环境，形成了阿拉伯人迫于生计而四处奔波的沙漠型生活方式和特性：他们热爱那无路可寻的沙漠和寸草不生的秃崖，可是又渴望离开这一切，向肥沃富庶的地区迁移，四处征战。阿布・泰马木在《坚贞诗集》中形象地揭示了阿拉伯人的这一特性：“你抛弃自己的故乡，不是为了寻找天堂，却是那面包和椰枣，把你吸引到了远方。”(注：尚劝余：《伊斯兰远征动因试析》，载《阿拉伯世界》，2000年第2期，第62页。)<br />
&nbsp; &nbsp;&nbsp; &nbsp;就社会状况而言，当时的阿拉伯半岛尚处于原始部落氏族制向阶级社会过渡时期，即恩格斯所说的以大规模掠夺战争为特征的“英雄时代”。阿拉伯人的“英雄时代 ”深深打上了游牧社会独具的烙印，因为当时阿拉伯半岛大多数居民是游牧民“贝都因人”。为了争夺牲畜、牧场、水源和抗御外来侵袭，游牧部落之间经常互相攻击，战火连绵不绝。沙漠游牧民中盛行尚武精神。劫掠被奉为一种民族风俗，是一种自然而合乎逻辑的行动，是一种标志强盛的风尚。抢人驼马，掳人妻女，抢劫商队，被视为谋生的途径之一，是经济上调节余缺的一种方式。倭马亚王朝早期的诗人顾托密曾以四句诗形象地揭示了阿拉伯人的这种早期生活原则和特性：“我们以劫掠为职业，劫掠我们的敌人和邻居。倘若无人可供我们劫掠，我们就劫掠自己的兄弟。”(注：希提著：《阿拉伯通史》，马坚译，商务印书馆1979年版，第 26页。)<br />
&nbsp; &nbsp;&nbsp; &nbsp;“英雄时代”特征和游牧社会特征的结合，使阿拉伯人养成了勇敢善战、争强好胜的民族心理。<br />
&nbsp; &nbsp;&nbsp; &nbsp;阿拉伯半岛的自然环境与社会状况决定和造就了阿拉伯人的民族性格，这种性格表现出巨大的复杂性和矛盾性：一方面他们能够克制和忍耐，另一方面又表现出暴躁和易怒；一方面注重自由、直爽、强悍、信义、勇敢、慷慨、抗暴、扶弱，另一方面又喜好掠夺、争斗、复仇。<br />
&nbsp; &nbsp;&nbsp; &nbsp;圣战不仅意味着征服、争斗，而且意味着反抗迫害、反对邪恶，这是阿拉伯民族双重性格合乎逻辑的反映，是阿拉伯民族禀性最富凝聚力的外化。(注：尚劝余：《伊斯兰圣战成因试析》，载《阿拉伯世界》，1991年第4期，第53页。)穆斯林历史上的伊斯兰圣战充分表明了这一点。圣战不仅是阿拉伯民族性格的充分体现，而且与伊斯兰教的产生、传播息息相关，是伊斯兰教和阿拉伯社会发展的必然产物。<br />
&nbsp; &nbsp;&nbsp; &nbsp;6～7世纪，阿拉伯半岛正处于新旧社会交替的历史大转折时期，氏族部落之间的矛盾，奴隶与奴隶主之间的矛盾，外族入侵导致的民族矛盾等错综复杂地交织在一起，使阿拉伯半岛陷入不堪重负的内忧外患之中。各种基本矛盾的不断激化，孕育着一场重大的社会变革。实现阿拉伯半岛政治上的统一，已成为阿拉伯氏族部落的共同愿望，成为历史进程的客观要求。这一统一要求反映在意识形态上就是用一种新的一神教取代四分五裂的各部落的多神教。穆罕默德顺应历史发展潮流，挑起了阿拉伯半岛统一重担，创立并传播新的一神教――伊斯兰教。一神教的主张触犯了麦加贵族的政治、宗教、经济利益。穆罕默德和穆斯林遭到了残酷的攻击、刁难和迫害，最后被迫离开麦加，迁居麦地那。麦地那遂成为发展伊斯兰教、与信仰多神教的麦加贵族相抗衡的堡垒。穆罕默德在麦地那宗教公社初步巩固、条件成熟后，鉴于阿拉伯半岛的统一大势，旋即组织穆斯林大军，进行了一系列圣战，其中他亲自带兵出征的达27次。631年，阿拉伯半岛基本统一，政教合一的国家初步形成。从此，圣战使伊斯兰教迅速传播，成为一种震撼世界的力量。<br />
&nbsp; &nbsp;&nbsp; &nbsp;《古兰经》反映了这一圣战形成的大致脉络。麦加时期，是穆罕默德艰苦创教的时期，因此麦加章的口气是温和的，号召信徒要避开多神教徒和不信道者，或者以地狱的苦难告诫世人，以天堂的享乐许诺信道者。这是由当时穆斯林的处境决定的。但是，麦地那章的语气较之麦加章的语气就大不相同了，充满了信心和勇气。麦地那章对所有反对信仰伊斯兰教的人表示了极大的愤怒，号召穆斯林同他们斗争并杀死他们：“你们要与他们战斗，直到迫害消除，一切宗教全为真主!”(8：39)“你们当为主道而战!”(4：84)这类号召对异教徒进行圣战的词句在麦地那章中大量出现，并越来越占有重要地位，成为伊斯兰教的重要组成部分。法兹勒・拉赫曼曾指出，在麦地那强盛时期，“除了礼拜和完纳天课，大概没有什么事情比圣战更受到重视了”(注：托马斯・李普曼：《伊斯兰教与穆斯林世界》，陆文岳和英珊译，新华出版社1985年版，第145页。)。<br />
&nbsp; &nbsp;&nbsp; &nbsp;可见，圣战是伊斯兰教产生、传播和阿拉伯社会历史进程的必然产物，是反抗多神教徒迫害、侵犯，并战胜多神崇拜，完成阿拉伯半岛统一，加速阿拉伯社会发展的合乎逻辑的必然结果。<br />
&nbsp; &nbsp;&nbsp; &nbsp;从上述伊斯兰圣战缘起和形成过程的追溯中，我们可以看出，伊斯兰圣战的实质就是维护和传播伊斯兰教，它所针对的：一是不信教者，二是压迫者即邪恶。这一实质表现出明显的双重性：一是扩张征服，二是抵御邪恶，亦即一方面体现出进攻性，另一方面体现出防御性。这种双重性和阿拉伯民族性格的双重性是一致的。<br />
&nbsp; &nbsp;&nbsp; &nbsp;综观穆斯林的历史，有两类性质不同的圣战：一类是早期扩张征服的圣战；另一类是反抗迫害和侵略的圣战，即多齐所说的“圣战只有在伊斯兰教的敌人发动进攻时才进行；如果有谁按别的意思来理解《古兰经》的规定，那么，肯定是错误的”(注：约・阿・克雷维列夫著：《宗教史》(下卷)，乐峰等译，中国社会科学出版社1984年版，第147页。)。四大哈里发时期和阿拉伯帝国时期的伊斯兰远征、奥斯曼帝国的征服等，属于前者；穆罕默德对麦加贵族的战争、萨拉丁对十字军的抗击、阿富汗对前苏联入侵的抵抗等，属于后者。<br />
&nbsp; &nbsp;&nbsp; &nbsp;圣战的和平是一种在穆斯林“社会与社会间”的关系，至于这一关系如何扩展到现代意义的国家与国家的关系，即意味着对伊斯兰传统思想的挑战。值得一提，伊斯m教的世界^,社分煞N社,伊斯m之舍c之舍,世界L期χ牛穆斯林有嗦Z他充奴`。他的和平是指δ滤沽值暮推,不是指教徒。<br />
根据伊斯兰教义，“圣战”是“为主道而战”即穆斯林的护教“战争”，是所有伊斯兰教徒（穆斯林）最重要的宗教义务之一。《古兰经》记载：“为主道阵亡的人，你绝对不要以为他已死去，其实他们还活着，在真主那里享受给养…谁为主道而战，以致杀身成仁，或杀敌致果，将赏获重大的报酬。”阵亡的殉道者称为“烈士”，得到最大激励，还能永住乐园，成为穆斯林的最高斗争理想。<br />
<br />
《古兰经》中允许战争的经文表明，战争只能作为击退侵略和铲除暴政的手段；军事胜利不应导致扩张和控制财富，或者使某个种族凌驾于另一个之上。在伊斯兰教看来，伊斯兰国家受命在地球上确立正义，而这要求穆斯林们面对不义和压迫并且要根除它们的原因，而不是掌握地球和奴役人民以及主宰他们的福利，而是没有任何可疑意向地在地球上确立真主的话语。《古兰经》要求信众们为真主的事业而战，不夹带任何世俗的意向。“恶”意味着超越了限度。当人们趾高气扬地奴役他人和剥夺他人的权力并且占有财富时，就超越了限度，战争就是为恶而战的战争。<br />
古兰经教义以仁爱和平公义为主，但也容许在公义的前提下，发动“圣战”。中世纪随着阿拉伯人的文化水平日渐提高，甚至超越同期的欧洲。然而因“圣战”观念，他们自始就产生了一个暗杀军政要人的传统，也是当代激进伊斯兰恐怖主义组织的历史根源。<br />
<br />
伊斯兰先知穆罕默德死后，集宗教、政治和军事于一身的继任者哈里发(&#1582;&#1604;&#1610;&#1601;&#1577;)由各领袖公举。伊斯兰教的历史中，最开始的四位正统哈里发除第一位外其余三位都是被行刺而死。自第四位哈里发“阿里”开始，伊斯兰教分裂成三派即正统派(d尼派)(Senni)、哈瓦立及派(Kharajites)和什叶派(shia，徒众名shiltes)。阿里被“哈瓦立及派”于661年刺杀；阿里派系遭到操控大马士革军政权的穆阿维叶的清除，穆阿维叶(Muawiyah，第五位哈里发)自封为哈里发并建立了倭马亚王朝(Umayyad)，至此哈里发已成世俗的军政领袖；“阿里派系”残余否认穆阿维叶的权威并组成什叶派(shia，徒众名shiltes)退往伊拉克，后到波斯(今伊朗)建立根据地，以恐怖主义行动攻击其它伊斯兰教徒。<br />
<br />
伊斯兰教的恐怖主义暗杀传统，到在10世纪前后，从“什叶派”分离出一个更激进教派“亦思马因”(Ismails)，其教规奉教和专门刺杀敌人，被其他国家称为Malahidas，中文史籍称为“木剌夷”（木剌夷国）。木剌夷国弱人少，但靠专业暗杀令各国畏惧。在叙利亚这教派名为Has Chischin，读音为Assissin，即为“刺客”一词的辞源，“木剌夷”完全靠恐怖主义立国，后为蒙古西征时摧毁。<br />
<br />
十字军东征后，伊斯兰世界遭到侵略，“圣战”观念受到大肆吹捧，其时组织了一批被称为Mufahedin自杀敢死队，意即“信仰战士”，专门以自杀方式攻击敌人。当今这个词汇被阿富汗游击队所采用，先是反苏联，继而是反美和反西方。阿富汗的塔利班组织，就是这种“信仰战士”的部队，原属阿富汗南部的Durrani部族，信奉巴基斯坦激进逊尼派的迪奥宾(Deoband)派，追求纯正古兰经教训，反对非伊斯兰国家干预国家事务，要求赶绝所有外国人，支持者大多为地区性的部族，都是单纯而未见过世面的农民和牧民。其教规重视清修，与世隔绝，不准信徒读书、照相、看电视电影，连运动、下棋、放风筝等游戏都不容许。由于激进逊尼派，与激进什叶派势极端对立，曾企图攻灭什叶派圣城 Mazar-i-Sharif (阿里墓所在地)与伊朗亦剑拔弩张。其背后则受巴基斯坦及沙特阿拉伯的激进逊尼派支持，军力约2到3万人。沙特富商本・拉登与之趣味相投。拉登为也门人及巴勒斯坦人后裔，父亲为富商，曾就读于沙特King Abdulaziz大学(King Abdulaziz University)读管理。在阿富汗抗苏时，资助巨型防空洞，其所住的山洞竟装有冷气机。其兄Salim1989年在美国得克萨斯州意外身亡，拉登仇恨西方特别是美国文明。在1991年美军进驻沙特阿拉伯，攻击伊拉克。拉登十分愤慨，认为沙特是先知的圣地，包括圣城麦加及麦地那在内，是不容许非伊斯兰教徒践踏，因而极端反美。<br />
