																											<div id="postmessage_382407" class="t_msgfont">以下转自中评网：<a href="http://www.china-review.com/gat.asp?id=23274" target="_blank">http://www.china-review.com/gat.asp?id=23274</a><br />
<br />
<br />
多党民主是个坏东西 <br />
――用产权理论破解“民主的公地悲剧” <br />
 方绍伟&nbsp; &nbsp;<br />
<br />
采写：王永章（《汕头都市报》执行主编）<br />
 <br />
受访者简介：方绍伟，美籍华人经济学家，芝加哥“制度经济研究中心”主任（<a href="http://www.ieCenter.org" target="_blank">www.ieCenter.org</a>) ，华中科技大学经济学院客座教授，2009年十佳“中国读本”《中国热---世界的下一个超级大国》作者；方绍伟毕业于国际关系学院英文系、北京大学经济学院；1994年赴美攻读政治学和信息管理学；曾就职于中国社会科学院美国所 ，曾任北京“天则经济研究所”副所长。<br />
 <br />
多党民主不是好东西、产权理论可以破解“民主的公地悲剧”、“一党立宪”可以……，这是美籍华人经济学家方绍伟先生近日在国内多所大学演讲的主题。他自称“离经叛道”的这些言论和令人“拍案叫绝”的独特论证，引起受众的广泛兴趣和议论批评。日前，我先后在汕头专访和“越洋信访”了已回到芝加哥的方绍伟先生。<br />
 <br />
多党民主是个坏东西<br />
 <br />
记者（以下简称记）：近日你在国内几所大学的演讲中说，“民主是个坏东西”。民主确有诸多缺陷，但这个结论是不是太笼统？因为现代民主概念不仅包含了权力来源，也包含了宪政，即对权力的限制。<br />
 <br />
方绍伟（以下简称方）：其实更准确点，应该说“多党民主是个坏东西”。现代意义的民主，确实是一种多党竞选、轮流执政、妥协冲突的“宪政民主”，它是一套解决权力来源和权力制约的政权“程序机制”，不管它能否或在多大程度上能真正实现“主权在民”的“实质理念”。<br />
<br />
在宏观政权意义上，这种企图避免政治独霸的自由竞选制度，可以称为“多党民主”或“议会民主”（简称“民主”或“民主制”）；在微观治理机制上，它包含着某种一般性质的约束权力使用的法治宪政工具，这种一般性质的治理体制可以称为“问责民主”（简称“问责”或“问责制”）。“问责民主”是横纵向的“政治问责制”的重要内容，也可以说是我们常常提到的“社会主义民主”应有的内容。宏观政权意义的“多党民主”与微观治理机制的“问责民主”的相对分离，是理解现代多元政治发展的一个关键。<br />
<br />
需要特别指出的是，民主的问题不是“确有诸多缺陷”的问题。是从人类文明发展史透视民主的“未为人知的本质”的基础上，我才得出“民主是个坏东西”这个结论的。<br />
 <br />
记：但我觉得市场本身就有民主成分。比如比尔盖茨，他很富很有权，但他的富有和权力还不是满足了千千万万消费者的需求，使他们自愿地自下而上投出货币选票得来的吗？这不是民主是什么？如果否定了民主，那岂不是也否定了市场？抑或，你所希望的真正的民主恰恰是市场这样的民主，而非现有的政治民主？<br />
 <br />
方：市场与民主的区别就是经济自由与政治自由的区别，从经济自由的合理性和合意性推导出政治自由的合理性和合意性，这一直是一种西式的错误观念。经济自由与政治自由的区别很多，经济自由从来都是某种政治制度规范之下的自由，政治自由则因为“公地悲剧”而留给世人一个致命的盲点。<br />
<br />
如果用“货币选票”来定义“市场民主”，从而导出“市场民主”以及“政治民主”的合理性和合意性，这正是我所指的“市场原教旨主义”和“民主原教旨主义”的一个谬误。这里的道理并不复杂，但确实需要一种超越常识的思维。<br />
<br />
美国的大投资家巴菲特经常引用他老师格雷厄姆的一句话说：“市场短期看是一台投票机，长期看则是一台秤重机”。许多人只知道这句话是“价值投资理论”的精髓，却看不到它一语道破了“市场民主”和“政治民主”的天机。明确地说，巴菲特之所以那么成功，完全是因为他看透了“市场民主”的根本缺陷。都说“群众的眼睛总是血亮的”，可巴菲特比别人更能赚钱的秘密却在于认定：“群众经常是疯狂的，市场经常是错误的”。巴菲特“逆向思维”的赚钱秘密“不幸”也被现代心理学、大脑科学、行为金融学和神经元经济学的研究所证实。<br />
<br />
这第一层含义是“市场民主”的缺陷，第二层含义则是“政治民主”的缺陷。市场短期的问题是“货币选票”引起的，这恰好表明“投票机”这台“民主机器”在政治运用上也会有同样的问题。实际上，“政治投票机”本身就有一种反智、反德能和反经验主义的倾向，它鼓励的是一时的表现、一时的感觉、一时的痛快，这就是为什么西方的政治家首先都是煽动家的原因。煽动家有的是“马上上台”这种超强的“时间偏好”，但民主制的致命之处还不是竞选，而是通过竞选表现出来的、以政治上的“时间偏好”去强化市场中的“时间偏好”的反文明倾向。<br />
<br />
所以，不能简单地迷信西方意义的“市场民主”和多党竞选的“政治民主”，其中的“市场原教旨主义”和“民主原教旨主义”值得加倍警惕。<br />
 <br />
记：现在有很多学者主张回归传统文化，并从中寻找中国社会发展的道路，这好像符合哈耶克的自发秩序原理，但似乎也有文化决定论的味道。你怎么看这个问题？<br />
 <br />
方：1840年的鸦片战争后，中国人先以为是在“船坚炮利”上矮人一截；洋务运动和甲午战争失败后，又继而认为“船坚炮利”须以“议会民主”为支撑；清末民初的“君主立宪”和“议会民主”流产后，又觉得求“议会民主”而不得是“制度文化”上有问题。可谁又能对文化直接下手呢？<br />
<br />
这就是我所说的“中国文化的现代化悖论”：不完全自我否定则无法自新，若完全自我否定则又无所凭以自新。在想全盘否定和不能全盘否定之间，大家最终回到了“可操作”的政治制度上。问题是“五四运动”以来，“议会民主”在中国太深入人心了。什么东西越求之不得，就越追之心切。但中国人又大多是圆滑和现实的“现世主义者”，每每在现实政治目前碰壁，都会有人提出类似“曲线救国”的主张来。<br />
<br />
在我看来，在具体的社会发展道路问题上提“回归传统文化”，其实是一种糊涂观念。它的潜台词是，西方的制度不好，中国的现有制度也不好，所以我们要回到“传统文化”中挖出更好的东西来，至少要找出能使中国的现有制度更加完善的东西来。可这样问题就来了，包括“现代新儒家”在内的所有主张“回归传统文化”的人，恰恰在“正式规则”意义的“硬件制度”问题上没能从“传统文化”中挖出任何明确的东西，因此只能在“非正式规则”意义的“软件文化”上含混其词，旁顾左右而言他。如果不能直面“硬件制度”，“软件文化”的讨论就多少给人以“无奈”、“玄虚”和“托辞”的感觉。<br />
<br />
从主客体及其相互关系看，广义的文化概念可以包含物质、精神、技艺和制度等四个方面的内容。文化决定论只有具体化才能有说服力，否则你就无法解释同一个朝鲜文化，为什么会有南北朝鲜那样的巨大差别。在某种意义上，哈耶克的“自发秩序原理”是对新古典经济学暗含的“完全理性”的一种纠正，但是，中国的“哈耶克热”很可能热过了头，许多人没有看到哈耶克自己的整个理论体系的自相矛盾，也没能看到“自发秩序原理”可能走向了一种“完全有限理性”的极端。<br />
 <br />
肯定现有制度是“回归传统文化”的出发点<br />
 <br />
记：其实你关于“全盘中化”的观点走得更远，你的理由是，中国传统文化中不仅有“中庸之道”，更有“有恒产者有恒心”这种优良因素。但遗憾的是，两千多年来，“有恒产者有恒心”并未落到实处，落到实处的反而是“利出一孔”，也可能恰恰是这个思想阻碍了中国市场经济和民主政治的发育。如此，怎能“全盘中化”？<br />
 <br />
方：“全盘中化”一方面是相对“全盘苏化”和“全盘西化”而言的，另一方面更加重要的是，它是相对于“对中国现有政治制度的非常识化肯定”而言的。这里要强调两点，一是我对中国现有政治制度的肯定是一种“非常识化”的肯定，我的解读不同于一般的观点，也不同于中国官方的说法；第二点是，肯定中国现有政治制度是“回归传统文化”的出发点，只有明确肯定中国现有的政治制度，“回归传统文化”才有合理的立足点，否则就说不清我们的“回归”到底是“硬件制度”的“回归”还是“软件文化”的“回归”。肯定了现在的“硬件制度”，“回归”传统的“软件文化”就意义重大，这是一种肯定了发展、肯定了现在的制度创新的“回归”，是一种寻求完善已有的制度创新的“回归”。<br />
<br />
显然，对现有“硬件制度”的肯定也就是对传统“硬件制度”的否定，而传统的君主制则正是“利出一孔”的基础，它自然不可能使“有恒产者有恒心”落到实处。<br />
<br />
全部问题的关键之处在于，我们几千年来对人类政治制度的认识，一直停留在亚里斯多德的“以统治者人数区分政权”、把政权分为君主制、贵族制和民主制的陈旧观念上。如果我们确立一种“以产权属性区分政权”的新观念，就可以把政权分为“政权私有”的君主制、“政权共有”的一党制和“政权公有”的民主制，就可以由此对各种政权的“产权特征”进行有意义的非常规分析，从而得出否定君主制和民主制的结论。“全盘中化”正是在这一“离经叛道”的理论背景下提出来的。<br />
 <br />
政治自由竞争的本质是“政权的公地悲剧”<br />
 <br />
记：在芝加哥学派看来，现在的政府，其很多职责本来是可由市场去承担的。但听你的演讲，似乎你有让一个政党将其自身作为私产（公司）来对待的意思，这个观点更新鲜。可在我看来，如果一个党派真能把自己当私产去经营，那也只能解决“内部问题”。而假如一个政党在政权上处于垄断地位，竞争这个“外部问题”怎么解决？没有竞争，光靠责任心能靠得住吗？比如中共现在的问责制，究竟能在多大程度上约束自己？<br />
 <br />
方：确立“以产权属性区分政权”的新观念，要紧之处是用我们的“实证理性”去战胜我们的“规范冲动”。经济学的芝加哥学派有一个重大缺陷，那就是它的“自由市场规范”压倒了它对政府的起源、本质和功能的“实证认识”，仿佛现代意义的市场是可以游离于政府的“超自发秩序力”似的。<br />
<br />
同样，“主权在民”的“规范冲动”太多了，只会妨碍我们对民主的内在本质的真正认识。多党民主是一种企图避免政治独霸的自由竞选制度，民主制度中包含的经济自由之外的政治自由有着极大的现实吸引力和道德感召力，西方民主的全部精妙之处和忽悠之处就在于“政治竞争”。有些人把民主乌托邦、民主原教旨、民主拜物教、民主迷信、民主虚伪等等批得头头是道、理由十足，可他们就是无法说服听众，因为在常识里，有政治自由总比没有政治自由好，多一些政治自由总比少一些政治自由好，甚至即便是虚伪的自由也总比没有虚伪的自由好。这正是当今中国和世界上所有的民主怀疑论者和批评者的共同困境，我称之为“民主批评者的难题”。<br />
<br />
丘吉尔“民主次优论”的名言一再被人引用的原因，那就是因为，说西方民主的不好、说民主在非西方实践的不好、说在条件不成熟时搞民主的不好、说民主没有“普世版”而只有“各国版”等等，即便说破了天，到头来还是回落到政治自由问题上，尤其是社会行动层面的言论出版、集会选举、结社组党等自由。问题的全部关键就在于，政治自由竞争的本质到底是什么？除了我们通常说的没能说服人的“政治自由的虚伪性和文化特殊性”之外，政治自由存不存在更具本质性的严重问题？显然，这是解读中国现政权和西方民主制的重点。<br />
在我《中国热》之后的下一本书《多党民主的悲剧》所阐述的“产权国家论”里，政治自由“最大化”的西方民主，由于“政权的公地悲剧”而使政治自由出现了常识所未能理解的大问题。在政权公有的民主制中，外在性质的问责制（ACCOUNTABILITY）由于内在性质的责任心（RESPONSIBILITY）的低下而强化，但这种强化了的问责制反过来却由于责任心的涣散而不可能解决一些大问题。比如一直困扰着美日等西方国家的财政赤字和公债问题。为什么以问责制的有效性而自鸣得意的西方，偏偏难以解决一个最起码的“财政责任”问题呢？如果不能看到“政权的公地悲剧”，西方社会的许多“后现代困境”就无法理解。<br />
<br />
正是在这个基础上，特别是在对比了民主制和一党制的“产权特征”的基础上，我才认为中国的现有政权制度是行得通的。如果更进一步对比君主制或独裁制的“产权特征”，中国的现有政权制度则更是我们中国几千年来最优的选择。我们没有认识到的，当然并不意味着它就不是我们最有幸而得到的。<br />
所以说，如果光靠政治竞争就能靠得住，那我会双手赞成西方多党民主制；可如果政治竞争偏偏就靠不住，那只有“政治责任心”最可靠。一党制的“政权共有”正是解决“政治责任心”的有效体制。当然，内在性质的责任心，并不可能自动解决外在性质的问责制。但责任心确实是问责制的基础。<br />
当代中国的最大问题，就在于能否在挡住“多党民主”诱惑的前提下稳步推行“问责民主”。<br />
 <br />
“外在性质的问责”应当如何建立<br />
 <br />
记：既然你承认内在性质的责任心“不能自动解决外在性质的问责制”，那么这个“外在性质的问责”应当如何建立？仅仅一个“责任心”怎么就能让你对现有的制度这么有信心？<br />
 <br />
方：在中国现有的一党制下，“向上问责制”比较有力，但“向下问责制”和“横向问责制”依然比较弱。从一党制的产权特征看，这是很好理解的，因为一党制下的执政党本身就是一个“附随型”（ADHERENT）组织，不象西方的“竞选党”那样是一种更松散和更“契约化”（CONTRACTUAL）的组织。现在许多人在大谈“党内民主”，应该提醒他们好好理解什么叫“附随型”组织。<br />
<br />
“横向问责”的一种阻力来在于对“分权制衡”的反感，但中共上层早晚会看到，“向上问责”中存在的“隐蔽信息”和“隐蔽行为”会使上层不堪重负，为真正确立上下级“委托-代理关系”中的上级控制力和信息优势，“横向问责制”的创新只是时间问题（深圳试行的“行政分权”只是“内部分权”，不是“制衡”意义的“外部分权”）。“向下问责”的具体机制不仅仅是“选举制”，选举的缺点很多，政治协商、人大议政、信访来访、地方议事会、媒体监督、网络议政等等都有改进的余地，“向下问责”的制度创新同样是当务之急。<br />
<br />
除了“责任心”之外，我对现有制度的信心主要来自于二十年来“一党制的宪政发展”。有些天真可爱的美国人至今仍在预言，说中国会出现历史上的农民起义或社会崩溃。他们看不到的是，中国历史上的王朝更迭悲剧来自于上层，来自于君主制的终身化和家族化特征，使得政权的交接受困于“家族基因”。而非私人化、寿命永久化的现代政党特征，解决的正是这种困境。中国“一党制的宪政发展”表现在“限任化、全民化、非私人化和制度化”的“执政党四化改造”上，其中的限任化已经有明确的“年龄、年限和任期限制”基准，这样的一党制实际上已经解决了权力绝对化、终身化和家族化等等导致君主制和独裁制出现“基因悲剧”的难题。另一方面，它也避免了民主制下由于责任心的弱化导致的“公地悲剧”。<br />
 <br />
民主政权的“公地悲剧”是根本不可能化解的<br />
 <br />
记：你的意思是说，现代民主国家的政权因为太“公有”，从而减弱了它的责任心，造成诸多问题不能解决或解决不好？这个问题更有想象力啊。那么请你请解释一下“政权的公地悲剧”悲在哪里，有可能化解吗？<br />
 <br />
方：“公地悲剧”是经济学里的常识，一块自由开放的公共草地，大家都能使用，也可以协调管理，按照简单的“直接思维”，个人在公共草地上放牧，并没有排除其他人可以同样使用，悲剧何在？但“间接思维”告诉我们，当草地不归某一具体的个人所有，而只属于一个抽象的集体时，个人在使用时就会给草地带来一种伴随性的“无补偿损耗”或“负外部影响”（“外部性”）。 自由使用包含着一种“争夺刺激”，如果想多得好处，你就得想方设法抢在别人之前多使用公地，就得千方百计增加别人使用公地的各种成本，就得挖空心思把资源从需要付费的地方挪到这个不需付费的地方。稀缺和争抢导致了拥挤，导致了公共草地的逐渐退化。“公地”问题涉及环境污染、资源滥用、人口爆炸、网络空间等“公共领域”，美国经济学家埃莉诺・奥斯特罗姆，就是因对“公地”管理的研究而获得2009年诺贝尔经济学奖的。<br />
<br />
在西方多党民主制度下，国家政权成了可以自由争抢的公共草地，一个无人觉察的制度化了的“政权公地悲剧”就上演了。我将在《多党民主的悲剧》一书里详细阐述这个“产权国家论”，其中对西方多党民主制的分析，将涉及民主的公地逻辑（非排他与稀缺、无情与必然），政权的公地悲剧（自由特征、败德特征、短期特征），民主的社会倾向（政客化、赤字化、公债化、非人化、堕落化），民主的制度特性（理性的无知、理性的偏见、理性的操控、理性的掠夺），民主的历史趋势（政治投机主义、短期行为主义、媚俗反智主义、超前消费主义、平等民粹主义）和民主的文明陷阱（双重时间偏好、公民文化的衰落）等等六个层面。<br />
<br />
我对民主制的彻底否定的原因就在于，民主政权的“公地悲剧”是根本不可能化解的。因为真正的悲剧不是鲁迅说的“将人生有价值的东西毁灭给人看”，悲剧不仅在于鲁迅说的毁灭价值本身，更在于毁灭价值的过程和方式。用英国哲学家阿尔弗雷德・怀特海的话说，悲剧是“郑重其事的无情”加上“徒劳无益的逃避”，是一种当事者不管能否认识到的被决定了的不幸命运。<br />
<br />
民主悲剧当然也是人类文明的悲剧，因为全世界人民一个个都一直在兴高采烈和欢欣鼓舞地、选择一个对自己不利的社会政治制度而仍未自知。飞蛾扑火时，他们看到的是光明，不是火焰。<br />
 <br />
“一党制的宪政发展”是“中国道路”的最佳选择<br />
 <br />
记：中国改革开放的路径选择其实受芝加哥学派影响很大，那时经济学家的想法是，先明确产权、进而建立市场、从中培养中产阶级、最后导出民主宪政。从西方经济政治的理论逻辑和实践经验看，这种路径选择是正确的吗？或者说，从市场导出民主宪政是普适的吗？在有些人看来，中国30年的改革中普通人既没分享到多少成果，似乎也未验证上述“普适价值”。这种判断正确吗？如果不正确，那是因为30年时间太短，还是有更深刻的原因？<br />
 <br />
方：就目前而言，西方的经济政治理论既逻辑混乱、又缺乏说服力，特别是表现在传统的“市场化导致民主化”的“现代化假说”，以及最近的“民主导致经济社会发展”的“诺斯命题”上。从西方的历史实践经验看，是精英法治或精英宪政引致了工业革命和经济发展，随后才是大众民主的全面扩大。因此，是契约和宗教色彩的政治文化之下的精英宪政导出了市场经济和随后的“宪政民主”，而不是“市场导出民主宪政”，所以也就谈不上“市场导出民主宪政是普适的”。<br />
<br />
从以上的简单的理论归纳看，目前的模糊认识是严重和普遍的。说“中国30年的改革中普通人没分享到多少成果”，这显然不符合我们的经验观察。如果是指分享西方意义上的“多党民主”的成果，那“成果”确实没有，但“问责民主”意义上的“成果”还是有的。在现有制度下，“向上问责”在加强，“向下问责”在悄然起步，权力制衡式的“横向问责”也可见端睨。<br />
<br />
然而，“多党民主”的冲动是不可低估的，政治自由有一种内在扩张的“公地逻辑”，许多非西方国家就是在这种逻辑的支配下，走上了社会动荡之路而仍未自知其所以然。民主可以来得很快，也可以来得很慢，来得快的未必不是灾难，来得慢的则未必是福音。走向“多党民主”就是灾难，走向独裁就不是福音。中国的“一党制的宪政发展”应该是“中国道路”的最佳选择，尽管我们仍处于起步阶段。<br />
 <br />
中国的制度建设和改造应当法治先行<br />
 <br />
记：现在很多人主张必须先政改，民主也好，宪政也罢。问题是，从欧美的正面经验和印度拉美的反面经验来看，都存在一个由什么人来政改（比如修法立法）的问题。欧美是中产主导，拉美则是穷人主导（中产阶级人数很少，也难进议会政府），所以效果截然不同。这是不是佐证了先实行市场经济（培养中产）再实行宪政民主的正确性？抑或二者可以同时实行？<br />
 <br />
方：你的问题似乎仍然首先暗中设定了“西方民主是人类社会最终的政权形式”这样一个“福山命题”。我提出的“产权国家论”就是直接针对这个命题的。“一党制的宪政发展”或“一党立宪”在我看来是完全行得通的。但问题并不仅仅在于对君主制、民主制和一党制的“产权特征”的认识，更在于大的世界观的改变。<br />
在国内几所大学的演讲中，我发现问题根本不是理论逻辑，而是被“洗了脑”的那种对民主的痴念。以前是反对民主的人不讲逻辑，现在我发现是崇拜民主的人不讲逻辑。好在我的“产权国家论”有一部分专门阐明“公地逻辑”如何决定民主崇拜者的“信仰发生机制”和“信仰持续机制”。<br />
<br />
前面说了，欧美的立宪是“精英立宪”，不是“中产立宪”，现代意义的西方“宪政民主”是在“精英立宪”后经过市场经济而“扩张”出来的，这在美国的从“白种男性富人”开始的民权发展史上表现得最为清楚。“精英法治”导致工业革命和经济发展是一个“英国秘密”，但成功实践“英国秘密”的美国并没有真正掌握“英国的秘密”。<br />
<br />
由于工业化过程与民主化过程并行，人们产生了“经济发展导致民主化”或“民主化导致经济发展”的“民主错觉”，而根本看不到是法治化保证了经济自由和经济的发展，是法治化使自发遵守不确定结果的民主规则成为可能。由于精英政治不能保证会渐进成宪政民主，在后来者国家可以有借鉴和缩短先行者国家经历的“后发优势”时，人们就产生了不顾“法治先行”、用偏激的“大众民主”去代替渐进的“宪政民主”、以为“宪政民主”便万事大吉的“民主幻想”。<br />
<br />
印度和拉美以及其他非西方国家的“穷人立宪”便是这种“民主错觉”和“民主幻想”的双重结果。在我的分析框架里，问题已经不是民主和市场那个先行，而是法治要先行。但与其他“法治主义者”不同的是，我的观点以“政权的产权实证分析”为前提，我强调“一党立宪”的可行性，同时也承认各国的立宪道路的历史局限，并不是任何国家都可以象中国这样有幸走上“一党立宪”之路的。<br />
历史有时很无情，它看上去既是自发的，又是建构出来的，它可能摇摆于偶然性和必然性之间，甚至也可能是一种“认识到的被决定”。<br />
 <br />
把政治制度的选择归结于“国民性”是一种理论无能<br />
 <br />
记：托克维尔考查美国后得出一个重要结论：民情比法治和环境更有决定意义。在他看来，美国民情的一个核心就是他们的自治传统。这种传统决定了他们倾向于对政府权力施加更多的限制。而中国的民情是依赖皇权思想严重。这是不是你主张一党立宪的原因？<br />
 <br />
方：民情、国情或“文化土壤”是重要的，但把政治制度的选择归结于“国民性”就既武断草率，又是理论思维软弱无能的一种表现。“一党立宪”早在1980年代就有人提出来，但从一开始到现在的所有论证，都是围绕着“承认现实”而展开的。这就是为什么民主的狂热者指责它是“御用”的原因。显然，光有这种“承认现实”的“实证理性”还是远远不够的。真正的“实证理性”应该在整个人类历史中寻找一种以理服人的经验逻辑，并且在经验意义上承认“强权政治”的同时不以“强权政治”为理论依托。<br />
<br />
在我这里，“一党立宪”是在对比分析君主制、民主制和一党制的“产权特征”后得来的。一党制克服了君主制的“基因悲剧”，克服了民主制“公地悲剧”，但却面临着严重的“问责危机”。一党制能否在当代世界的制度博弈中进一步发展，关键还要看它的“一党立宪”能在多大程度上克服“问责危机”。<br />
<br />
与对政权的“产权特征”的认识相比，民情国情的考虑在政治制度的选择上应该是第二位的。以民情国情为理由拒绝“多党民主”不是一种有说服力的做法，以“多党民主”的“悲剧逻辑”为理由拒绝“多党民主”才是一种更有竞争性的理念。不直面“多党民主”的道德优越性而破之，“一党立宪”就会给人留下“强权逻辑”的把柄。<br />
 <br />
政治竞争与市场竞争有根本区别<br />
 <br />
记：政治市场上的一党立宪等于经济市场上排斥其他力量介入，这是垄断。所以，你不怕不解决准入和竞争这样的外部问题，跟你信奉的芝加哥或新制度学派的理念相冲突吗？<br />
 <br />
方：你问到点子上了。包括芝加哥学派在内的“新制度经济学”或“现代制度经济学”，在经济自由和政治自由及其区别上，认识的清醒程度是不同的。一般而言，“现代制度经济学”主要包括：布坎南的公共选择和立宪经济学，诺斯的制度变迁论和开放机会论，奥尔森的集体行动论和坐寇政权论，科斯和威廉姆斯的交易费用论和产权契约论。<br />
<br />
布坎南在这个问题上最为清醒，比如他说过：“市场竞争是持续不断的，每一次采购都可以在彼此竞争的几家商号之间进行选择。政治竞争则是断断续续的，一次选择要管两年、四年或六年。市场竞争可以允许若干竞争者同时存在，即使有一家占领了市场的大部分，仍然会有少数顾客会光顾自己喜欢的卖家。政治竞争则是非胜即败，一家赢了就独霸这市场若干年。在市场竞争中，买方可以相当有把握地知道他采购回来的是什么东西。政治竞争则并非如此，在这里，选民作为顾客可以说是购买一个代理人的服务，可是他并不能约束这个代理人遵照合同办事，相反，是被迫授予这个代理人相机行事的广泛权利。政治家不同于私营店主，选民是无法让他们负责履行诺言的。”<br />
<br />
概括地说，政治竞争与市场竞争的根本区别在于竞争间歇性的不同、输赢零和性的不同、兑现承诺风险的不同。正因为看到了这些不同，布坎南对民主及其危机的认识比谁都深刻，他因此能进一步从政治决策的集体性和外部性，去分析民主政治竞争的公共选择危机和财政困境。但是，布坎南没有强调市场决策和政治决策在产权基础上的不同（市场中的产权是私有或国有，民主政权的产权是公有），他因此没能明确地把民主制度与政权公有联系起来，并进一步推导出民主政权的“公地悲剧”。<br />
<br />
正是因为这一点，“现代制度经济学”对政治竞争局限的认识就很不彻底，诺斯在2009年的《暴力与社会秩序》一书中提出的“开放政治经济机会论”就是很好的例证，对此我在别的场合已作了批评。对政治竞争的不彻底认识与“现代制度经济学”在市场竞争上的“达尔文主义”倾向正好是对应的。<br />
 <br />
没有产权的绝对权力才是真正的绝对腐败<br />
 <br />
记：你的意思是，经济市场上竞争与产权几乎同等重要，而政治市场上产权比竞争更具决定意义？<br />
 <br />
方：这就是关键点。实际上，经济市场也好，政治市场也罢，没有纯粹好的竞争，也没有纯粹坏的垄断，一切要看具体的制度和制度运作。在“产权国家论”的显微镜下，许多以前看来是“垄断”和“独霸”的政治负面特征，其实恰好是一党制的优势。铁的事实和逻辑是：在政治市场上，产权比竞争更可靠，因为西式政治竞争的本质，就是拿“社会成本”而不是拿“私有产权”进行竞争。有时我不禁纳闷：认识这一点怎么就这么难？一党制“政权的产权明确程度”正好介于君主制和民主制之间，这同市场竞争里“产权不是越明确越好”的结论是非常一致的。许多人迷信“用权力制约权力”，殊不知，用没有产权的权力制约其他权力，正是西方民主制出大问题的一个原因。都说“绝对的权力绝对地腐败”，其实，没有产权的绝对权力才是一种真正的绝对腐败。显然，整个西方的政治经济理论同民主制一样都出了大问题。<br />
<br />
我想我已经表达了对我所信奉的“现代制度经济学”的不满，我更期待一种更少政治文化偏见的“后现代制度经济学”。<br />
 <br />
中国政府内部的现实主义精神比想象的强得多<br />
 <br />
记：教科书上对工业革命的过程和工人阶级的生活描述得比较阴暗和凄惨。其实，根据研究，那在很大程度上是文学家、历史学家的夸大，是痛恨资本主义的知识分子的不实之词。中国现在的舆论也类似。由于理想主义情节和对市场、民主理解上的偏差，一些知识分子喜欢强调改革的负面效果，网上尤其“群情激奋”（当然，改革本身也有很大问题），把经济学家骂得很凶。也可能是照顾了这种民粹气氛，产权改革似乎停顿甚至走回头路了（比如铁本案、山西煤矿整合中的国进民退）。你感觉是这样吗？如果是，那是否表明高层对市场建设这个目标有了动摇？<br />
 <br />
方：对市场、民主的理解的偏差和幻想是毫无疑问严重存在的。民粹主义同市场原教旨主义一样可怕，网络自由的滥用也正好暴露了一种电子空间里的“公地悲剧”。但是，经济学家中的“市场万能”倾向或市场原教旨主义谬误，确实是民粹主义“揭竿而起”的一个原因。市场原教旨主义在“出招”时要么把中国的现实设想成“真”市场状态，要么把“价格调节”当成社会调节的唯一工具。至于说改革是否已停顿甚至走回头路，高层对市场建设这个目标有了是否动摇，我想还不至于此，个别案例所表现的可能只是中国经济特征的复杂性和市场化的限度。<br />
<br />
中国政府内部的现实主义精神比我们想象的要强得多。当然不能排除有些人在民粹主义、市场原教旨主义和民主原教旨主义上有模糊认识。但与民粹主义和市场原教旨主义相比，民主原教旨主义可能更为危险。比如有的经济学家已经公开提出经济改革差不多到头了，剩下的只是修修补补问题，是到了民主化政治改革的时候了。如果对宏观政权意义的“多党民主”与微观治理意义的“问责民主”不加区别，单单含混地认定“民主是个好东西”，危险性就特别大。因为民主的狂热者会因为你的“民主条件论”而说你“干打雷不下雨”和“打着民主反民主”，而民主的反对者则会把“问责民主”连同“多党民主”一概否定，留下民主的同情者无所适从。<br />
<br />
有人把教育、医疗、住房等问题说成中国“新的三座大山”，这表明民生问题更加突出了。但正是在这样的民生问题上，市场化和民主化的取向会变得更加敏感和重要。作为一个制度经济学家，我对市场原教旨主义和民主原教旨主义都深怀警惕。<br />
 <br />
中共的高明之处在于它一直都在寻求和解<br />
 <br />
记：六十年来的确有很多人祸，执政党也因此背上了包袱。那么在转型时期，你认为应当如何对待这些历史问题？包括你的汕头同乡丘岳首先生在内的不少知识分子，主张走南非道路――和解。你觉得事态有没有严重到需要提出“和解”的地步，和解的可能有多大，怎样和解，是不是需要执政党首先有个姿态，对“零八宪章”签名的人只抓了一个是不是一种姿态呢？<br />
 <br />
方：其实中共的高明之处正在于它一直都在寻求和解，只不过有些人的“激愤情绪”和“规范冲动”妨碍了这种认识。如果认定只有交出政权才是真正的和解，那问题就不仅仅是天真了。<br />
<br />
历史问题在我看来是最清楚不过的，清末民初以来的中国政府一直解决不了的最基本问题，无非就是“挨打挨饿”式的内外忧患问题，是中共解决了这个基本问题。毛泽东是解决这个问题的关键人物，在1921成立后没有毛泽东主导的前14年，中共自己的前途未卜，更不必言承载国家的命运；但从1935到1949年的后14年，毛泽东领导下的中共却取得了令俄国人、美国人和蒋介石都无法想象的胜利。<br />
<br />
毛泽东的胜和蒋介石的败，关键问题都是“产权”，即土地的产权和军队的产权。毛泽东的土地政策解决了中国“土地产权太集中”的民心问题，蒋介石看到了这个问题却解决不了这个问题；蒋介石的政治策略解决不了“军队产权太分散”的军阀文化下“军权私有”的问题，毛泽东“党指挥枪”的政治策略解决了这个问题。前者事关人心向背，后者事关战争胜负，前后两者的互相加强使中共取得了惊人快速的空前胜利。<br />
<br />
正因为毛泽东伟大，对他的个人崇拜是发自内心和油然而生的，由此产生的错误和灾难，怎么能只算在他一个人身上呢？个人崇拜根本就不是一个“主体现象”，它是一个主客体互动的“集体现象”，受益受害的是双方或多方而不仅是一方。和解是一种妥协，只要别人认账而自己不认账，那就不是妥协。<br />
历史的帐有四本，第一本是“国家建设”，第二本是“政权建设”，第三本是“市场建设”，第四本是“精神建设”。毛泽东解决了“国家建设”问题，但没能解决后三个问题，邓小平及之后的领导人解决或正在解决“政权建设”和“市场建设”问题，但整个民族的“精神建设”仍是问题。如果我们一味苛求历史，我们看到的就仅仅是问题而不是成就。苛求毛泽东解决“政权建设”问题，就像苛求邓小平解决“精神建设”问题一样，都是对历史和时代局限的强求。 <br />
&nbsp; &nbsp; <br />
实际上，新世纪以来“一党制的宪政发展”已经使中共变得前所未有的强大和稳定。我从美国回来讲学，有人说我“用极右的理论得出极左的结论”，是中共的“地下党”。其实错了，套用“小沈阳”调侃的话，这叫“说西方人的话，让西方人无话可说”，我们老祖宗说“以其人之道还治其人之身”；认真地讲，中共已经到了可以靠自身的制度力量坚定有力地运转的时候了。我甚至可以说，中共的强大已经到了它可以更加开放和无需寻求理论辩护的程度。相形之下，我们的认识都已经大大落后于现实了。<br />
 <br />
最担心仍有太多人对民主抱幻觉和空想<br />
 <br />
记：你似乎对中国未来很乐观。那么，在你看来，“一党立宪”已经从问责制开始了，接下来将会如何走？这个“宪”最终何时能“立”成？你心目中是否有个时间表？<br />
<br />
方：对中国未来乐观的原因上面已经说过了，但我要强调的是，这是一种对“制度”的信心。许多人就是认定中共搞不好，但他们凭的是情绪和痴念。我不担心一党立宪如何走好、何时能立，我最担心的是，中国政府内部仍有太多的人对民主有幻觉、有空想，这种冲动造成的“内部自毁力”，将会极大地破坏中国崛起的进程。在当今世界的制度博弈中，丢分的总是那些“内部自毁力”强的制度。我只希望，中国政府内部的现实主义精神能最终压倒“民主原教旨主义”的盲动。<br />
 <br />
记：你的“产权国家论”的确很有颠覆性，但因时空限制，你还没有具体谈到在这一理论框架下如何防止滥权和腐败，以及如何保证经济增长。希望有机会看到你在这两方面上的突破性论证，并减轻或消除人们扬弃旧理论、接受新观念时的折磨。<br />
 <br />
方：好，有机会我们再谈“产权治腐论”和“经济发展制度论”。 <br />
<br />
