													<h2>我和网络警察的一次见面会</h2>
						
						
																											<div id="postmessage_56321" class="t_msgfont">先说一下自己，俺是西北一首府城市的报社小编辑，终日夜班。2009年之前，一直做国内新闻。通过大量国内新闻，人生观和世界观发生改变。后来经常看钱组长的BLOG，并以正面新闻反着做，使劲做而乐此不疲。<br />
<br />
2009年，报社调整，我开始负责本地新闻的编发。大概是1月10日，我在浏览本地网站的时候，发现一条有意思的线索。有网友在一论坛发帖，图片中有一酒店开张，有当地的一县城税务局的横幅表示祝贺。当时一看，觉得是一个线索。立刻给记者打电话，让他去采访。可以想象的是，这条新闻应该是这样的：1，记者目击现场，某县税务局发横幅庆祝酒店开张。2，采访县税务局，领导肯定会说不知情。<br />
<br />
记者去了现场，给我打来电话说，发不了。原因是横幅不只县税务局的，挂在大楼上方的还有包括部级党委、政府、市级党委、政府以及县级党委、政府的。尤其是，还有浙江省委、省政府的横幅庆祝（该酒店老板是浙江人）。当时我觉得不管杂样，拍张照片留念。其实当时就有了既然本地报纸发不了，那就发到网上的念头。<br />
<br />
后来照片拿到了手，我就在1月14日的凌晨0点左右的时间，于本地网站上传了图片，并发到了天涯论坛。可惜当时俺没有兴趣小组的ID啊，否则我就发到这里了，也不会有下面的事了。<br />
<br />
其实当时还是多了个心眼的，我穿着“拽着毛驴跑”的马甲，在报社单位里发帖。同时，我也给钱烈宪投递了一份，14日早晨他就发到了他的博客里。到了1月15日，按照我的估计，是《信息时报》的记者李斌看了老钱的帖子，就写了一篇新闻。详见：<a href="http://news.163.com/09/0115/04/4VM31TO700011229.html" target="_blank">http://news.163.com/09/0115/04/4VM31TO700011229.html</a><br />
<br />
类似于这样的新闻太好写了，不用采访对象，只需要把帖子内容和网友议论的内容一写就成了。在当天，各大网站的社会新闻头条都是《网友曝俱乐部开张34个政府部门道贺》。16日，中青报也发了评论文章。当时心里还挺爽，随后就出事了。<br />
<br />
过了几日，一天早晨，我正在睡觉。忽然有哥们给我打电话，他供职于另一家报社，说是公安厅网监大队的两个警察正在查这件事情，让我注意点。当天下午，这些网络警察就通过我发帖的IP找到了我们单位――可以肯定的是，天涯肯定出卖了我的IP地址。<br />
<br />
当时我有点傻眼了。老婆刚大肚子，哥们不会被关进去吧。赶紧在老钱的博客上留了个言，我估计俺要是真出事了，老钱也能帮我在他博客上喊喊啊。<br />
<br />
下午在我们主任办公室，俩警察穿着便衣，态度比我想象中的好。他们问我，知道为什么找你吗？我装傻，答，不知。他们说，某日某时某分，你在天涯上发帖了。内容是如何如何如何……我当时点头，是啊，没错。有什么问题吗？<br />
<br />
一警察说，最近我们正在开两会，你这事引起了很坏的舆论效果，你知道吗？<br />
<br />
我还是装傻，不知道啊。<br />
<br />
问：照片是谁拍的？<br />
<br />
我心想不能买了我们记者，就说是我自己拍的。<br />
<br />
另一警察问：你发帖的初衷是什么？<br />
<br />
我继续装傻：好玩啊。我在编国内新闻的时候记得深圳某个区的酒店开张，有很多政府的祝贺条幅。这次我见了，也觉得很好玩，就想发到网上让他们开开眼。（同志们啊，我虽然马上要当爹了，可长相幼齿，穿上校服可以混进中学。）<br />
<br />
我假装反问：这有什么问题吗？<br />
<br />
一警察说：你发帖没有什么失实的地方，但是跟帖中有很多很不好的信息。（原来是有人发帖搜出了我所在的省区有一个副省级领导在该酒店开张的时候是到场嘉宾，并对此进行了攻击和辱骂）<br />
<br />
警察又问：那个《信息时报》的文章，是不是就是你写的？<br />
<br />
我笑了，摇头。<br />
<br />
后来，警察询问了我的身份证号码（我故意说错了一个号），问了我的家庭情况，问了我经常使用的电子邮箱和QQ、MSN等等，这些我觉得无所谓就如实告之了。<br />
<br />
再后来，俩警察也问不出什么，就走了，临了还给我发了一根烟，意味深长的说，你也是新闻从业者，一定要遵守宣传部的相关规定。为什么要有宣传部，那就是因为有些东西能发，有些东西不能发。我继续装傻称哦。<br />
<br />
随后，单位领导批评了俺，说是没有将此事给领导说一声。应该由他们来面对网络警察们的询问，并称他们有狗屁权力来调查――这里有点小感动。<br />
<br />
这件事情可以给大家带点经验<br />
<br />
1，发这种帖的时候，千万别去天涯，别的论坛我不知道，但天涯出卖发帖者的IP那是经常有的事情。<br />
2，上传图片的时候，最好去国外网站，真理部本事再大也查不到？（这句话求证）<br />
3，老钱的博客影响力很大――这是废话<br />
4，面对网络警察的询问，装傻是必要的，装明白那结局就不知道了。――请各位理解，俺老婆当时知道这消息吓坏了。<br />
5，请各位继续补充。<br />
