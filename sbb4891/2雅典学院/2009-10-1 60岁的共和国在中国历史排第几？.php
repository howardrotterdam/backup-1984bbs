													<h2>60岁的共和国在中国历史排第几？</h2>
						
						
																											<div id="postmessage_265804" class="t_msgfont"><a href="http://blog.ifeng.com/article/3223161.html" target="_blank">http://blog.ifeng.com/article/3223161.html</a><br />
<br />
<img src="http://blogfile.ifeng.com/uploadfiles/blog_attachment/0910/49/1370449_859b793c13fbf14b69ef5dfd8e3cdb06.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
我认认真真地等着时针越过2009年10月1日零时，然后开始恭恭敬敬地写这篇博客，我觉得这样做很有意义，虽然我并没有什么可激动的，但也感觉到自己正跟着这个国家进入一个新的时间，在我写这篇博客的时候，这个全称为“中华人民共和国”的统一的多民族的位于亚洲大陆东部的国家已经走完了它第60年个年头。<br />
第一个甲子落幕，第二个甲子开启。<br />
中国古代文化和科学的神秘中，为什么要把60作为一个计数区间？这就是一种神秘中的神秘，至今我没看到有人能解释清楚。<br />
不管在共和国60年的不长历史里出现过多少不幸的事，多少不愉快，多少未解之谜，多少被人诟病的缺处，我不得不老实承认，我还是由衷地甚至可能是深沉地爱这个国家，尽管我并不确认自己一个轻浮又轻漫的人对这个国家的爱是不是真的很深沉。<br />
共和国走过了第一个甲子，我好像跟着长长舒了一口气，似乎在此之前我一直在心里默念契诃夫所写的那篇《装在套子里的人》里达丢夫常挂在嘴边的一句话：不要出什么乱子吧？<br />
是啊，不要出什么乱子吧？<br />
这个国家的某个角落每每出现一点儿动静，我或者我们都会在心里默念这么一句，一直到它坚持到第61年的这个第一天。<br />
大家可以舒一口气了。<br />
翻开历史书，居然发现某些冥冥之中让我和我们为这个国家终于舒一口气的理由，即建立60年的中华人民共和国，在中国有史记载以来的所有统一的多民族的朝代国家里，正排在一个微妙的临近安全的界点上。昨天或者去年，历史里行走的这个国家还如履薄冰如临深渊，过了这个60年这一点，一切似乎要好得多。<br />
五帝之后，中国这块土地上有史可查的统一的多民族的以华夏或汉民族为主体的朝代如下：夏，商，周（西周、东周），秦，汉（西汉、东汉），晋（西晋、东晋），隋，唐，宋（北宋），元，明，清，民国。<br />
在上述朝代中，存续时间只有一个甲子的是：<br />
秦朝，公元前221-前206年，凡15年。<br />
隋朝，公元581-618年，凡37年。<br />
民国前期，公元1911-1949年，凡38年。<br />
西晋，公元265-317年，凡52年。<br />
即把现在的中华人民共和国放在中国历史上统一的多民族国家来观照，至少它的寿命可以排在这4个朝代前面了。<br />
若把中国历史上那些曾经在这块土地上完成近似统一的朝代也算在内，则现在的中华人民共和国又可以超越以下：<br />
三国・魏（公元220-265年，凡40年），南朝・宋（公元420-479年，凡59年）及齐、梁、陈，北朝的东魏、北齐、西魏、北周，五代诸朝（公元907-960年，凡53年）。<br />
中国历史上存续时间长过60年的朝代分别是：<br />
夏（凡500多年），商（凡600多年），周（西周800多年，东周500多年），西汉（公元前206-25年，凡231年），东汉（公元25-220年，凡195年），唐（公元618-907年，凡289年），北宋（公元960-1127年，凡167年），南宋（公元1127-1279年，凡152年），元（公元1206-1368年，凡162年，其中全国统一共89年），明（公元1368-1644年，凡276年），清（公元1616-1911年，凡295年）。<br />
中华人民共和国的这60年历史，除了表示它基本熬过了历史上那些为战乱纷争所困的短命王朝的存续时限之外，这个60年，似乎走到了另一个结点，在中国历史上，如果能存续时间达到60年，接下来它差不多一定会超越的目标则是：<br />
元的162年。<br />
以及，明的276年，唐的289年，清的295年，而之后则是两宋的319年，两汉的426年。<br />
若以元朝为参照，则至少要在现在的60年上再加100年，即使如南宋，比现在的中华人民共和国也多出97年的时间存续，也就是说，当中华人民共和国的存续历史越过现在的60周年之后，在已经的历史规律支撑下我们或可以断言，这个叫做中华人民共和国的国家至少还有100年的寿命。<br />
那时，现在所有的人可能都死光了，他们更没法知道共和国最终的寿命是多少。<br />
当然，既有的历史不是所有的历史，既有的规律也不是完全的规律。<br />
或者在国家历史上，60年一甲子应被称为“建国折腾期”，说如果一个国家熬过这60年的“折腾期”，国家形态、国家财富和国家文化可能被打造坚实，则其后经年，即使是没有什么改革开放没有什么宏人大圣，吃老本也能再坚持100年不倒。<br />
这就是历史规律。<br />
其实，这样规律的科学性还是有的，别的不说，就我知所，在NBA历史上有一条最颠颇不破的真理是，在总决赛7场比赛中，历史上没有任何一支球队可以在1比3落后的情况下反败为胜的。<br />
这一句套用到中国，在中国历史上，还没有任何一个朝代在存续时间超过60年的情况下，最终寿命不接近或超过160年的。<br />
所以，我看涛哥完全可以于今天上午站在天安门城楼上宣布，走过60年历史的中华人民共和国，已经进入中国历史上的160年以上寿命的朝代俱乐部了。<br />
所以，今天的中国人在欢度60周年国庆的时候更应该多想想如何为后世开创一个长达100年的更和平、更安全、更富足、更幸福的国家和时代，60年共和国的江山，说不上是铁打的也是合金的，社会革命在今天已经没有什么实际效用，改良和改造才是实现中国社会进步和民生幸福最小成本的手段，也是历史在今天给我们的真正暗示。</div>

							
							
							
							
