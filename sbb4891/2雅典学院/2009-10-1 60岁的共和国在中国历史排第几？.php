即使在封建时代和奴隶时代，人们也总是说当时的时代是最好的，是最先进的，是最伟大的时代，但在现在看来，它们多么落后、保守和丑陋啊。<br />
只有时间的流动是永恒的，这才是大智慧，人类只要不死绝，社会形态就不会穷尽，中华人民共和国也同样得和大秦帝国或者大明王朝被放在一个篮子时吆喝，我们看不到了，但可以想像得到。<br />
