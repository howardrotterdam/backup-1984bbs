不要太把国家当成国家。<br />
不要太把现在当成现在。<br />
也不要太把自己当成自己。<br />
它们只是物质的一部分，现在只是时间的一个片段，而这个国家也不过是历史中的一个章节。<br />
那个完全可以凌驾于它们之上的大规律一直在的，谁也逃不掉的。<br />
