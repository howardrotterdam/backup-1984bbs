对于文章发表后的一值和回答的疑问，现回答于此：<br />
有人以为，不能用本文这种类似算命的方式看待历史问题，我倒想说，这有什么不能这样看待的，既然你可能喜欢在电话号里多8而不喜欢多4，为什么国家就没有资格享受这样的待遇。<br />
