													<h2>警惕韩寒</h2>
						
						
																											<div id="postmessage_602175" class="t_msgfont"><a href="http://blog.sina.com.cn/s/blog_53d349a30100i3jb.html" target="_blank">http://blog.sina.com.cn/s/blog_53d349a30100i3jb.html</a><br />
<br />
一、2008年<br />
<br />
1，要了解韩寒，一定要通读韩寒的新浪博客。如果通读韩博，你很快会发现一条重要的分界线――2008年！<br />
<br />
 <br />
<br />
2，在2008年之前，韩寒博客虽然也有涉及时政话题，但多数时候，他的博客还是“私人习作”范畴，多为个人生活，赛车，文化圈事件；2008年之后，韩寒的博客重心明显转移到“公共话题”（尤其时政话题）<br />
<br />
 <br />
<br />
3，2008年，是韩寒从自我走向大众的分水岭。韩寒的上述转型，比较陡峭。比如07年底的博文《上海一些不合理的交通设施》还蛮平和，摆事实；但08年之后，几乎再无这样平和的时政文章<br />
<br />
 <br />
<br />
4，我个人一直比较欣赏韩寒。当我梳理韩寒全部博客时，我才发现自己的这个“一直”，其实恰好也是从2008年开始。<br />
<br />
 <br />
<br />
5，也就是说，至少对我而言，韩寒是因为从2008年开始关注公共话题，才成为现在之韩寒；否则对我来说，韩寒和郭敬明并无高下之分，都是成功的文化精英，只是文学风格的差异<br />
<br />
 <br />
<br />
6，持续关注公共话题并发表言论，就是“公共知识分子”。2008之后的韩寒，所作所为全部是“公共知 识分子”的POSE。这是一个客观存在。更进一步，这就是韩寒的主要价值<br />
<br />
 <br />
<br />
7，但是韩寒多次强调自己只是“竖子”，拒绝“公共知识分子”称谓。究其原因，大众受了太多所谓“专家”所谓“公共知识分子”的愚弄，使得“公共知识分子”在网络大众的眼中是一个“脏词”。同时由于民众的草根立场，“竖子”这样略显江湖气的词语，反而是很好的头衔。韩寒聪明的向大众讨了个巧<br />
<br />
 <br />
<br />
8，但无论什么称谓，公共知识分子也罢，或者喜欢议论时政的“竖子”也罢，除了“享受”媒体关注粉丝追捧等等“利益”外，同时也必须承担最基本的责任――尊重事实，尊重理性。<br />
<br />
 <br />
<br />
二、露馅<br />
<br />
1，韩寒博客的内容，在2008年后非常“锁定”在社会热点问题，特别是网络热点话题。哪里有热点，哪里就有韩寒！<br />
<br />
 <br />
<br />
2，韩寒博客的文字，语感极强，尤适网络阅读。同时，文字幽默、讽刺、善用暗喻等等。总之，从技术角度看，韩寒的杂文文字国内一流。<br />
<br />
 <br />
<br />
3，韩寒博客的立场，几乎都占在公众认为的“弱势”一面。同情弱势，对抗强权。<br />
<br />
 <br />
<br />
4，永远的“热点”，一流的“文字”，正确的“立场”，这就是韩寒。这就是韩寒的魅力。<br />
<br />
 <br />
<br />
5，我自己也因为上述三点，而从不太关注韩寒，到成为韩寒的粉丝之一。尤其是其第三点“立场”。每次读韩寒的文章，总感觉这哥们咋说得那么对呢，咋说到我的心坎上了呢。但这样次数多了，直到韩寒的两篇文章出来，我才恍然大悟<br />
<br />
 <br />
<br />
6，第一篇文章是关于“七十码”的《胡斌进去，胡彦斌出来》。这篇文章的背景，是网络谣传法院掉包，普遍不信任法院。韩寒的文章压根不去分析事实，而是绕口令一般继续传递不信任。韩的不信任与公众的不信任呼应强化。由于韩寒与当事人有一面之缘，当时我就想，如果是我遇到这样的命题，我会怎么写文章？基本上，我只有一个写作方式，就是根据我的“一面之缘”，去试图分析事实。但韩寒压根没这么做，他只是照例又“正确”地响应了网络公众的情绪。这篇文章，让我心里隐约不安。<br />
<br />
 <br />
<br />
7，第二篇文章是关于泰州的《孩子们，你们扫了爷爷的兴》。在文章中，韩寒压根没有真正试图去分析凶手杀人的原因，而是说，“这个社会没有出口，杀害更弱者成了他们唯一的出口”；“这些杀人事件的产生很大原因是这个社会不公正，不公平”。这两个说法，都不是理性的，但都是老百姓“爱听”的。读到这里，我才顿悟：要警惕韩寒<br />
<br />
 <br />
<br />
三、警惕<br />
<br />
1，重复一遍，韩寒的价值=永远的“热点”+一流的“文字”+正确的 “立场”<br />
<br />
 <br />
<br />
2，第一点可算“炒作”，但无可厚非；甚至于，对于公共知识分子的POSE，韩寒必须如此。在这点上，我完全支持他。<br />
<br />
 <br />
<br />
3，第二点可算“才华”，绝对值得敬佩。在这点上，所有玩文字的朋友都可以向韩寒学习。<br />
<br />
 <br />
<br />
4，需要警惕的是“正确的立场”。这里，韩寒的“正确”并非对事实的尊重，而是对公众情绪的迎合。你真的找不出一篇文章，韩寒是站在大众的“对立面”！这太可疑了。大众情绪难道永远正确吗？！肯定不是。（比如第一例中的“七十码”，事实证明法院没有“掉包”）但是，韩寒根本没有独立思考，他所有的“立场”就只有一条――公众的情绪！<br />
<br />
 <br />
<br />
5，一位网友说，“为老百姓说话”不等于“说老百姓爱听的话”。诚然！<br />
<br />
 <br />
<br />
6，上述所有对韩寒的批评，与韩寒的商业利益没有任何关系。即本文根本不讨论商业利益。韩寒目前的商业利益，都是他应得的。<br />
<br />
 <br />
<br />
7，本文只想说的就一句话：截至目前为止，韩寒根本没有独立思考，他所有的文章都是在迎合大众的情绪。并且，是“不论事实”地迎合。<br />
<br />
 <br />
<br />
8，本文，说与有心人</div>

							
							
							
							
