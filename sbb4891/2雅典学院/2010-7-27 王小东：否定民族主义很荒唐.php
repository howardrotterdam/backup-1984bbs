																											<div id="postmessage_761189" class="t_msgfont">中国“民族主义之父”王小东：否定民族主义很荒唐<br />
2010年07月26日 <br />
<br />
核心提示：《中国不高兴》的作者之一王小东就90年代民族主义的兴起发表了自己的观点。他说“民族主义之父”的标签虽带有贬义，但自己也并不排斥。他对“民族主义”有自己的坚持，他说，“否定它（民族主义）的真实性、真理性，这是很荒唐的。”此外，王小东对“逆向种族主义”作出批判，并认为“中国就是大国”，应在世界舞台上扮演自己的位置。<br />
<br />
王小东：民族主义的称呼发端于西方带有贬义<br />
<br />
凤凰网：小东先生被“冠名”很多，有人说你是“民族主义之父”，还有人说你“有人痛恨，有人封堵，然无人能忽略你的能量”。你怎么看待这些人给予你的这些标签和头衔呢？<br />
<br />
王小东：说“民族主义之父”怎么敢当呢？国外早就有民族主义，现代民族主义是在西方发端的，在中国要是说我是“民族主义之父”，那梁启超、孙中山往哪儿摆？这些东西都是别人给戴的帽子。有的是好意，有的是想骂你。叫什么“主义”有时候是大而化之的说法，如果要是知道我的思想、我的想法，看看我的书就知道了，《天命所归是大国》、《中国不高兴》，我在比如政治、经济、军事、外交、文化方面都会提出一些具体主张，至于这些主张是不是能够用“民族主义”这个词去概括都是有问题的。当然这个概括我们也理解，为了传播，有时要进行一些简化。<br />
<br />
本来我们从来没有拿这个词说来说去的，最开始我们是不说这个词的，我们只是表达自己的一些想法。比如说80年代那个时候很多的文人学者说，在英语当中是没有涉及“性”的骂人话的，我想其实你们年轻一代都知道，英语里面有涉及性的骂人的话，我当时英语比他们好一些，我就知道事实，所以我就出来反驳他们；还有一些人说，西方是没有酷刑的，只有中国才有酷刑，有人说只有中国人才喜欢观看死刑，其实西方的死刑还卖票呢，一直卖到大概是上个世纪初。<br />
<br />
当时有无数这种非常荒唐的说法，我当时就是看不惯，也是那句话--我可以不在乎你怎么看中华民族，但是最起码你别侮辱我的智商，你别侮辱我的英语水平。自从第二次世界大战以后，民族主义在西方有贬义，他们就把这个帽子扣了过来。但是后来我为什么接了这个帽子呢？因为这个无非也就是一个名字、一个符号。<br />
<br />
王小东：逆向种族主义者在侮辱人的智商<br />
<br />
凤凰网：你6月10日写的一篇博客，提到了美国丈夫因其中国妻子的逆向种族主义而离婚，这事儿挺有意思的，能向网友介绍一下吗？<br />
<br />
王小东：这也是我在网上转过来的，不是我写的。有一个跟美国人结婚的中国妇女，她坚持不相信“神七”那个事儿是真的，她说中国宇航员的太空行走是在游泳池里表演的。结果美国丈夫实在忍无可忍跟她离婚了。美国丈夫说了一句很经典的话，就是我不在乎她怎么看她自己的祖国，但是她不能侮辱我的智商。其实这个有可能是所谓的民族主义者编的故意恶心人的。<br />
<br />
其实“逆向种族主义者”这个名词确实是我发明的。中国人自己说自己是劣等民族就叫逆向种族主义。后面那句话我觉得甭管是谁说的，是美国人说的也好，还是中国人说的，我觉得是挺经典的，我套用了。<br />
<br />
80年代逆向种族主义者侮辱人智商的东西太多了，这种侮辱人智商的东西一直延续到现在，例子很多，顺手就拈来。比如说中国的房子比美国贵，中国的房子比英国贵，中国的房子比日本贵。咱们的房子跟咱们的工资相比确实很贵，这个问题很大，这个都了解。但是你要那么说，就是又侮辱人的智商了。<br />
<br />
你要说中国的房子比美国的贵，你是拿中国什么地方跟美国什么地方比？如果要是同等的话，比如说北京中心区跟纽约中心区比，哪个贵？北京中心区跟伦敦中心区比，哪个贵？跟东京比哪个贵？对吧？你要说讲一个工资比率那肯定是我们高，因为我们是发展中国家，人家是发达国家，发展中国家你所能购买的东西就比发达国家少。所以你要用工资比率来说的话，那肯定什么东西都是中国贵。这不用你说，这是废话。但是你要讲绝对价值，肯定是它们贵。不要说英国跟日本了，就美国也比中国贵，你问问美国哪国物价低，对不对？所以这是非常可笑的。这么愚昧的论调，竟然能够流行。<br />
<br />
我曾经在东京学经济学，它那个物价什么样，我还不知道吗？但是就有人坚持，还有在日本留学的博士坚持写文章说，中国的物价比日本东京的贵，我说胡说八道。说老实话，连日本生产的很多东西都是在中国便宜。我记得特别清楚，那时候还是80年代呢，那时候还有税呢，反正开头我把日本产的富士胶卷往中国带，后来我们大家都说看中国卖的富士胶卷比日本东京便宜多了。要是说白菜的话，我告诉你日本东京的白菜一颗切成八瓣，每一瓣都得卖十好几块人民币，你说哪个贵？睁眼说瞎话！一个西瓜合人民币得上百块钱，或者几百块，你说哪个贵？有这样说瞎话的，所以这个东西太侮辱人的智商了。我看不惯！<br />
<br />
王小东：90年代民族主义不该被否定<br />
<br />
凤凰网：你提到的这种为了哗众取宠而自我造就的这种逆向种族主义，以及这种自我矮化的论调，是从80年代就开始发现踪迹了，那么到90年代它有怎样的改变？<br />
<br />
王小东：这个东西一直到现在还有，包括我说的那种胡说八道。<br />
<br />
凤凰网：一直都没有变化过吗？<br />
<br />
王小东：不，有些人是坚持那个东西的，但是对比能发现很大的变化。在80年代的时候我说逆向种族主义不对头，那你在中国找不到一两个人响应的；90年代响应的人就很多了。刚才你不是介绍了嘛，我是《中国不高兴》的作者之一，我们那个书是畅销书嘛，当然有些人恶心，说他们买回去都是当擦屁股纸的，那我觉得他们那个皮肤够硬的，现在这个书的纸多硬啊。我想当然有人是买回供批判用的。可能有个别口味特别重的，买回去当擦屁股纸用了。但大多数人买这个书还是支持我们的。<br />
<br />
凤凰网：90年代初，民族主义开始在中国积蓄力量，有人说90年代民族主义的兴起有它的危险性存在，比如说极端民族主义这样一个因子，你又怎么看到当时民族主义的利弊呢？<br />
<br />
王小东：吃饭还能噎死呢，是吧？好多东西都不能吃多，是不是咱们就不吃饭了？睡觉还有睡过去的呢，那咱们不睡觉了？对不对？你要是这么说的话，那啥事咱们也别干了，有利有弊。这些东西我都觉得不构成批判，所以我觉得这些东西不是特别有必要回答。拿这个东西来否定这种思想存在的价值，来否定它的真实性、真理性，这是很荒唐的。<br />
<br />
其实在政治、经济、军事、国际、文化方面的一整套思想，一开始都是没有的。比如说最简单的，我发觉英语里面是有涉及性的骂人话的，你说这玩意怎么就有害了呢？而且好多人会英语骂人话不只是一句。而且好像还都能跟中文对应起来。其实说老实话我看人类也没太大差别。当时还有一个论调：有一种嫉妒叫东方的嫉妒。<br />
<br />
凤凰网：那也是90年代的论调吗？<br />
<br />
王小东：现在还风行呢。但是那个时候还当做一个重要的社会科学发现，说东方人是看见别人好就想把别人拽下来，西方人看见别人好就想赶上去。其实我89年、90年的时候，在加拿大居住，当时美国的民调就反应出美国人的心态，就是普遍看着谁想把谁拽下来，而且他们不掩饰，我就是这样。经济学家一般来说脑袋瓜还是比较理性一些，说咱们还是没必要自己受损失把别人拽下来，咱们还是自己上去好，但是大多数民众，包括哈佛大学这种博士什么的，都表达一种意愿，我们宁可自己受损失我们要把别人拽下来。<br />
<br />
所以说所谓的东方嫉妒只不过反映了人性的弱点而已，当时都说西方就不这样，西方特单纯，美国人特单纯。后来我说你们自个儿看看美国拍电影，怎么讲他们自己的，他们自己写小说怎么讲他们自己的，你就知道了。他们自己也不认为自己很单纯，英语还有一个词，翻译成中文叫“办公室政治”，其实这是字面翻译，要是译的话，就是勾心斗角。这些就是说当时搞糊涂了，我告诉大家不是这么回事，可是我说了出来怎么就有害了呢？怎么就危险了呢？给大家长知识也危险吗？当然有可能危险，但是我刚才说了，吃饭吃多了还噎死了呢，你知道英语里面有涉及性的骂人话，可能也有危险，因为到时候你要是不分场合的骂，到时候让人打了，一刀给捅了，那是够危险，你还是不知道为好。<br />
<br />
王小东：民族矮化自70年代至今还存在<br />
<br />
凤凰网：你还记得当时在90年代民族主义的口号兴起了之后，大家开始对于东西方有了自己的认识，有了内心的理解。你当时身边有没有一些朋友，第一次接触到民族主义，第一次认识到不应该自我矮化，他们会在具体生活当中有哪一些展现？<br />
<br />
王小东：好多细节我不记得了。但是我说过，我说我们中国人确实有一种过强的自卑感，比如说见了外国人就不自觉露出一种谄媚的笑，他后来反省了。他说我说得很对，他说虽然他自己还挺警惕的，但他有时候也是不自觉地见了外国人有一种谄媚的笑。当然这个跟我们历史的经历是有关的，毕竟鸦片战争是人家把你打败了，不是你把人家打败了。但是这种东西确实问题挺大的，挺严重的，你要说民族主义有危险，我说其实这些危险更大。<br />
<br />
我记得我们单位的一个外办主任，他一点不关心政治，也不关心这主义、那主义的，有一次就谈起来说，外国人对我们不怎么礼貌，不怎么好。他说我这十几年做这个外办工作经验，外国人对中国不礼貌，都是咱们中国人自己惯出来的。比如说我们搞大活动的时候，专门宣传就是说要对外国人如何如何，坐公共汽车上你见了外国人得让座。咱们见了外国老人让座，见了中国老人也应该让座，凭什么见了外国不是老人咱们也得让座。其实我就被人家叫过让座。但是那时候是比较早，我记得是1978年。<br />
<br />
1978年，我考上了北京大学，当时还很光荣。所以把这个校徽别在这儿，然后当时我跟我女朋友坐公共汽车，我记得特清楚是8路公共汽车。后来上了俩外国年轻姑娘，当时我是大学生，那两个年轻姑娘岁数比我还大一点，但是肯定没超过三十岁，也是年轻人。上来之后那个售票员就叫我跟我女朋友起来给他们让座，我女朋友没有任何思考，就很顺从的就站了起来，我就把她给拉下来了，我说不应该给她们让座，她们两个是年轻人。<br />
<br />
那俩年轻姑娘挺不好意思的，特别不好意思地说不坐。但那个售票员坚持要让我们两个让座，后来另外有两个人站起来让座的。后来那俩姑娘一个劲的道歉，开头不肯坐，后来坐下来了。那售票员看我那校徽，说你还是北大的呢，素质这么低。我说我告诉你，就是因为我是北大，所以我不能给外国人让座，这就是素质。<br />
<br />
当然那是1978年的事儿，但是现在这种事儿有没有？我告诉你，还有！我还在公共汽车上看见了，2008年！这有什么必要？对吧，像这样的事儿恶心不恶心？我觉得挺恶心的。当然现在老百姓自我维权的意识已经强，所以逆向种族主义已经有点行不通了。<br />
<br />
网上盛传的一张照片我没法核实，它有可能是photoshop出来的。就是说一个标语叫做“减少出行，为外国友人让路”。这是到处风传的，但是我觉得最起码像这种照片，风传本身体现了中国老百姓有了自己的维权意识，对逆向种族主义就反感了。这是我们中国土地。我们凭什么在中国土地上见了外国人就得让路，这不是太荒唐了吗？<br />
<br />
王小东：90年代民族主义影响国策最大在经济<br />
<br />
凤凰网：我们常常说民众意志在某种程度上会影响国策，你觉得90年代的民众意志是否对国策有影响？<br />
<br />
王小东：这么大的影响其实不敢说，坦率说，如果要讲影响，还应该是经济方面的，比如说我1990年底一直到1992年初，写过大量文章说，中国不能完全满足于在国际分工当中产业的低端，要提升自己的产业结构，要提高自己的技术水平，而且要用国家的力量，不能完全依靠市场。它涉及到一系列的措施，有很多的内参，涉及到一系列措施。<br />
<br />
1992年之后中国选择另外的发展道路，我们用历史来看的话，那个发展道路未必是错的，看来也挺正确，因为中国毕竟富裕了。但是当时的那种思想有没有价值？我认为是非常有价值的。2005年，我在英国伦敦经济学院发表过演讲，我当时在1990年底到1992年初，写过一系列文章，其实大部分是内参，少数是公开发表的，就是表达那种思想。但是后来中国没有选择这样一个发展道路。<br />
<br />
对于这一点，我认为自己没有什么沮丧，是赢的。为什么呢？如果中国1992年以后实行这条道路，如果我们先不搞产业升级，先是什么挣钱我们做什么，中国这条道路走过是正确的，我的理论是输了，但是我的民族是赢了，我还很高兴的。如果这条道路最后走了出了问题，不行了，那么说明我的理论是对的，而且我在很早之前就有远见，看出来这个后来的问题。那么我的民族输了，但是我的理论赢了，反正我总赢了一头。那么从我的民族来说，它要是输的话，它也没有完全输，最起码它可以回过头来再来看我的那些理论。<br />
<br />
后来，其实像我这种90年到92年这种经济学观点，在后来十年当中有很多是重新表达，他们可能比我表达更好，也就是说这个东西还真又被捡回来了，捡回来倒不是说我们中国就一定输了，而是说当时可能我开出那些药方的时候，中国作为一个人来说，身体还受不了这个药。<br />
<br />
凤凰网：也许这一剂药太猛。<br />
<br />
王小东：我记得当时有国家领导人说过，这种意思我们全懂，但是现在没钱怎么办？我们就是拿不出来这个钱。当然只有当家人知道有钱没钱。到了现在，你再回过头来看我20年前的那些思想，会发觉反而可能用得上，因为中国可能已经具备了用它的条件。中国现在是不是在一定程度上搞这些东西呢？中国就是搞了。当年比我还早发生那个争论的，比如说张爱萍将军的儿子张胜写过一本书。我认为他那本书最有价值的，恰恰是最后讲80年代很薄的那么几页，也是讲的这个争论。<br />
<br />
张爱萍将军的观点跟我是一样的，我比张爱萍将军晚，张爱萍将军的观点1985年就提出来了，但是当时我不知道张爱萍将军跟其他人有那么激烈的争论。但是其实讲的还是一个事儿，从现在来看的话，可能很多人认为我们还是做得不够，就是像张爱萍将军和我的这种观点，实行得还是不够。但是不管怎么说，我们也是这样开始做了。你就看到了，比如说你看到了神六、神七，你看到了我们的北斗等等，我们还是在做了。<br />
<br />
王小东：中国就是大国不必太在意西方脸色<br />
<br />
凤凰网：宋强先生曾经有这样的一个想法，他说1996年开始就不必和西方苛求改变那种和它们交流的关系了，这种平衡没有意义。你怎么看这句话？<br />
<br />
王小东：其实本来这个东西，不是说从96年开始，其实从很早就开始了。一开始我们说作为一个大国，我们确实没有必要那么在意西方对我们的看法，它们不高兴我们就改变自己。它们一说中国威胁，我们马上就说没有威胁，我们中国是一个小绵羊。没有必要这样。中国就是在比较弱的时候，八国联军入侵时代那就另当别论了，但是最起码在40年代往后，就是在比较弱的情况之下也用不着这样。中国实际上就是一个大国。不过我们也不能一点不看别人的脸色，因为跟外国有交往。<br />
<br />
凤凰网：有利益关系。<br />
<br />
王小东：但是我们不能这么样看外国人脸色，他们一不高兴我们急得不得了，一会儿说你们这个极端民族主义我们不安了，我们不愉快了，特别急！我记得那是1993年。也是一个大学者，还有一个国学大师也这么说，所以我对国学大师老是持有怀疑态度，你讲国学怎么还是逆向种族主义呢？有人表达了一点可以被扣上民族主义帽子的观点，这位国学大师刚从日本回来，马上就说，别说了，日本知识分子非常担心我们的民族主义。<br />
<br />
这时候我发言了，我说某教授你从这里看没看出中国知识分子跟日本知识分子的差距？他说什么意思？我说你看日本知识分子担心的是中国的民族主义，我说中国的知识分子怎么担心的还是中国的民族主义呢？按说你中国知识分子要向日本知识分子学习，应该是多担心点日本的民族主义，为什么你也担心的中国的民族主义呢？你要是学到家了，要是西方担心中国，在那儿嚷嚷中国威胁论，你就嚷嚷美国威胁论不就完了吗？实际上世界人民公认，美国对于世界的威胁比中国大多了，这是美国做的皮尤调查，是很有名的调查，主持人是美国前国务卿奥尔布赖特，反复说明的，世界人民认为美国对世界的威胁比中国对世界的威胁大。<br />
<br />
你看咱们一说中国威胁论咱们着急，而且来鞭打中国的民族主义，你说这玩意，咱们不说学习西方嘛，那你学啊！我特别赞成学习西方，所以像那种说民族主义反对学习西方，特别可笑。我就特别赞成学习西方，包括你发给我的提纲，说民族主义国家都是反对学习现代化的，都是反对学习西方的，这好像跟西方研究的理论也不符合。有一本也是很有名的研究西方、研究民族主义的书就讲过，它说发展中国家的民族主义者往往是非常西化的，他们是本国最西化的一群人，也就是说师夷长技以制夷，所以他们特别注意师夷长技；那些国学大师可看重传统文化了，不停地说我们是一种“和”的文化，我们不跟西方人偷，我们不能学西方人。不是那么回事，包括说民族主义者都是反对学习西方的，反对现代化的，这个还是请他们好好的研究一下西方的理论，向西方学习学习，西方怎么就不那么说呢？<br />
<br />
所以西方人的儿子和孙子，跟他的老爹、老爷还是有差距的。就是他们的这些干儿子、干孙子，其实并没有很好的把西方思想学到手。<br />
<br />
王小东：中国应承担更多的世界责任<br />
<br />
凤凰网：你觉得在这整个十年中，就是从80年代末到90年代初时间里面，经济大潮的涌入，各种各样体制的改变中，那段时间那十年所谓的民族主义有什么样的特殊性？<br />
<br />
王小东：其实我觉得90年代还是因为大家知识也多了，外语水平也提高了，经济还是一定的好转。当时一个是由于知识的增长，自尊心的增长，90年代对逆向种族主义是一种批判，但是其实还没有表现出很强的自信。你要是说它是民族主义，还不能说它是一种老话：什么悲情的、受虐的，可以这么说，包括有人注意到《中国可以说不》，它说中国谁也不想领导，就想领导自己，所以90年代还没有特别强的意识。<br />
<br />
你要真说变化，还是这个十年的变化，就是新世纪的十年，就是从2000年到2010年，中国确实钱多了富了，地位不一样了。当然很多老百姓还很穷，但是中国国家的钱多了，有些老百姓的钱多了也是个事实，所以自信就上来了。所以《中国不高兴》确实反映了这个现实。<br />
<br />
但是你要是阔了你是不是得多承担点责任，有更高的追求？所以我们说中国要领导世界。为什么？因为现在世界人民也希望中国提供领导。不是吗？美国它也很矛盾，咱们中国要一说领导世界，它心里酸，不痛快。一个是感情上酸，一个是理性地怕你损害了它国家的利益。但是另一方面说中国你不领导世界呢，出来骂的也是美国人，说你们老出来搭便车，说世界秩序全是我们维持，你在这儿光赚钱，让你出个兵你也不出。我们就特愿意出兵，上哪儿维和特愿意去，上索马里，有人花钱，包括我看凤凰卫视有些人说花钱花多了，我觉得这钱该花，一个是中国练练兵，一个也是为世界人民做贡献，去索马里还是远远不够的，我们要承担起更大的领导世界这个责任。<br />
<br />
我们在非洲发挥的作用就很好，像法国记者就写什么中国赴非洲被高度认可。我们现在非洲，只发挥一个经济领导的作用，也未必完全够，政治领导的作用也要发挥。漂亮话永远可以去说，但是我觉得我们老百姓不代表政府。我们不干涉内政，这是第一点，第二点不干涉内政要从西方人的角度来讲，他们没觉得你这个是特好的事儿。这一点中国老百姓和中国政府都应该明白，西方没觉得你不干涉内政就很牛。所以世界人民包括发达国家的人民，对于中国有干涉内政的需求，就是希望中国干涉外国内政的需求，它有这个需求。我们的中国作为一个负责任的大国，要满足这种需求。（书娣）</div>

							
							
							
							
