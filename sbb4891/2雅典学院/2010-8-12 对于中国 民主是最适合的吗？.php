													<h2>对于中国 民主是最适合的吗？</h2>
						
						
																											<div id="postmessage_780149" class="t_msgfont">大家记得大刘的黑暗森林法则吗：<br />
<br />
“宇宙就是一座黑暗森林，每个文明都是带枪的猎人，像幽灵般潜行与林间，轻轻拨开当路的树枝，竭力不让脚步发出一点儿声音，连呼吸都必须小心翼翼：他必须小心，因为林中到处都有与他一样潜行的猎人，如果他发现了别的生命，能做的只有一件事：开枪消灭之。在这片森林中，他人就是地狱，就是永恒的威胁，任何暴露自己存在的生命都将很快被消灭，这就是宇宙文明的图景，这就是对费米悖论的解释。”<br />
<br />
大刘同时提出了“技术爆炸”和“猜疑链”等概念<br />
<br />
<br />
大刘从宇宙尺度提出这个概念，就是这样，本质就是“弱肉强食”<br />
<br />
<br />
民A主中的“天赋人权”在我看来和“天赋君权”一样没有道理，<br />
试问：一个人，从出生到在工作之前，没有为社会做出贡献，更多的是从社会索取，<br />
他凭什么享有权利？<br />
<br />
民A主决策的质量暂可不论，但低效当不可置疑，<br />
不可想想古来军事如果由民A主来决策，是什么样<br />
同样，现今的公司，如果是民A主决策，恐怕也无法生存，<br />
<br />
市场瞬息万变，国家间也是如此，终有一日我们会走出地球，也是如此<br />
<br />
我心目中目前最好的制度就是“准独裁的执政官体系政治制度”<br />
如《文明》中所讲：<br />
执政官通过各级政A府机构掌控军政所有权力，行使这些权力不受任何系统的制肘，确保发展战略的一致性和高效运转；长老会不负责任何具体事务，只有权力弹劾执政官和担负甄选新的执政官的义务，目的是确保掌握权力的人在人格责任能力各方面的优异性；议会负责保证言论自由，要让所有的观点有机会传播，有机会传达到执政官的耳边，确保人类思想的多样性能够得到表达。长老会与议会在自己工作范畴内具有不受执政官干涉的独立性，长老会成员由内部推荐组成，议会成员通过民A主选举产生<br />
<br />
有人会问：<br />
<br />
“这样的制度，执政官的权力是不是太大了，如果谋取私利，很难有人能制约，长老会虽然名义上有弹劾权，但是，长老会的人数多，实际权力等于是分散了，根本无法与执政官相抗衡啊！”<br />
<br />
答案是：<br />
<br />
寻找一个值得信任的人要比寻找一群值得信任的人容易，同样，即使有人谋取私利的话，社会满足一个人的欲望也要比满足一群人代价小！而弹劾权本身只是一种威慑力。”<br />
<br />
<br />
民主是无奈的选择,是不得已的选择,没有办法的办法,<br />
<br />
民主和独裁没有好坏之分，只是适合不适合，民主最大的弊端是很多时候是低效的，<br />
（有个例子比如上次某人提到美国一条路修了30年，还有香港贪玩修路修了数10年的事情比比皆是，中国武汉高铁4年就完工）<br />
，你也可以想象如果中国实行民主，比如说修一条路，如果上万户民众中仅有一户不同意拆迁 而路就无法修下去的话，中国的现代化建设如何完成）<br />
而且中国几千年的奴化教育及目前残留的东方文化，民主，对于中国，不是最适合的<br />
<br />
在可以预见的时间内,窃以为中国还是独裁比较好(个人独裁),类似于《文明》 里的执政官制度, 执政官有各方面的最高权力,同时成立元老院,职能只有一个: 弹劾执政官<br />
<br />
在强有力的个人独裁下，中国完全也可以实现相对的清廉，以牺牲少部分人的利益，<br />
以民主所不能达到的高效，实现腾跃式的发展<br />
<br />
应51楼燕归来兄的建议，把”民主适合中国吗“改为”民主最适合中国吗？其他未改<br />
