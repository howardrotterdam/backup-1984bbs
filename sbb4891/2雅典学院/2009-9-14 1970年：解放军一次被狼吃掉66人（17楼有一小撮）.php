<br />
<br />
.......................巴图的口哨果然灵验，熟悉草场的马群立即意识到南面巨大的危险。整群马只停了一下，就开始集体转向，顶着狂猛的侧风向东南方向拼死冲锋。南有陷井泥塘，北有狂风恶狼，只有东南是唯一一条有可能逃命的活路。每匹马都瞪着凄惶的大眼睛，低头猛跑，大口喘气，一声马嘶也听不见了，马群中笼罩着跟死亡赛跑一样的紧张和恐怖。<br />
<br />
马群刚一转向，战局陡变。马群队形一朝东南，拳脚最少、防御最弱的马群侧面，就立即暴露在顺风冲击的狼群面前，而马群最具杀伤力的密集后蹄却被置于无用之地。狂猛的侧风也立刻减缓了马群的速度，削弱了马群抵抗狼群的武器。但是，侧风却使狼群如虎添翼。一般情况下，狼群速度高于马群速度，顺风逆风都是如此。在顺风时，狼快可马也不慢，狼要腾空扑上马身马背撕咬，不敢从马尾后面直接跃起，弄不好碰上一匹聪明马，它会突然加速，让狼扑上马蹄，非死即伤。狼只能从马的侧面侧身斜扑，才可能得逞。但狼侧身斜扑会影响速度，如果马速很快，狼就算扑到了马，也抓咬不住马，至多在马身上留下几处抓痕，狼的捕杀成功率也会降低。此刻，当马群不得不改变方向的时候，就给了狼群绝好的捕杀机会。<font color="red">狼群顺风追慢马，用不着侧身斜扑，只要狼在马侧面直身一跃，狂风就正好将狼刮到马背、马身或马颈上。狼就会用它的利爪不要命地抠住马身，用它的锋利钢牙迅猛凶悍地攻击马的要害部位，得手后立即跳离马身。如果马打算就地打滚甩掉狼，对付一条狼还行，可对付群狼只会更快送命。它只要一滚躺下来，一群狼就会一拥而上把它撕碎。<br />
<br />
马群发出凄厉的长嘶，一匹又一匹的马被咬破侧肋侧胸，鲜血喷溅，皮肉横飞。大屠杀的血腥使疯狂的狼群异常亢奋残忍，它们顾不上吞吃已经到嘴的鲜活血肉，不顾一切地撕咬和屠杀。伤马越来越多，而狼却一浪又一浪地往前冲，继续发疯发狂地攻杀马群。每每身先士卒的狼王和几条凶狠的头狼更是疯狂残暴，它们窜上大马，咬住马皮马肉，然后盘腿弓腰，脚掌死死抵住马身，猛地全身发力，象绷紧的硬钢弹簧，斜射半空，一块连带着马毛的皮肉就被狼活活地撕拽下来。狼吐掉口中的肉，就地一个滚翻，爬起身来，猛跑几步，又去窜扑另一匹马。追随头狼的群狼，争相仿效，每一条狼都将前辈遗留在血管中的捕杀本能，发挥得凶猛痛快 <br />
<br />
在惨白模糊的电筒光柱下，两个马倌又一次目击了几乎年年都有的草原屠杀。但这一次令人更加不能接受，因为这是一群马上就要参军入伍，代表额仑草原骄傲和荣誉的名马，也是马倌这么多年拼死拼活，提心提命养大的心肝宝贝。就这样眼睁睁地看着狼群连杀带糟蹋，巴图和沙茨楞连哭都哭不出来，他俩全身憋满的都是愤怒和紧张，但他们必须忍住、压住、镇住，竭力保住剩下的马群。但巴图越来越揪心，以他多年的经验，他感到这群狼绝不是一般的狼群，它们是由一条老谋深算、特别熟悉额仑草场的狼王率领的狼群，那些怀恨肉食被盗的公狼疯了，丧子的母狼们更是疯得不要命了，可是，狼王却没有疯。从狼群一次又一次压着马群往南跑，就可以猜出狼王倒底想干什么，它就是铆着劲，不惜一切代价想把马群撵到南边的大泡子里去，这是草原狼王的惯招。</font>巴图越想越恐惧，他过去见过狼群把黄羊圈进泥泡子，也见过狼群把牛和马赶进泡子，但数量都不算大。狼把一整群马圈进泡子的事，他只听老人们说过，难道他今晚真是撞见了这么一群狼？难道它们真要把整个马群都一口吞下？巴图不敢往下想。 <br />
<br />
巴图用电筒招呼了沙茨楞，两个马倌豁出命从马群的西侧面绕冲到马群的东侧面，直接挡住狼群，用套马杆、用电筒光向狼群猛挥、猛打、猛晃。狼怕光，怕贼亮刺眼的光。两个人和两匹马，在微弱无力的手电筒光下前前后后奔上跑下，总算挡住了马群东侧一大半的防线。马群从巨大的惊恐中稍稍喘了口气，迅速调整慌乱的步伐，抓紧最后的机会，向大泡子的东边冲去。马群明白，只要绕过泡子，就可以顺风疾奔，跑到主人们的接羔营盘，那里有很多蒙古包，有很多它们认识的人，有很多人的叫喊声，有很多刺眼的光，还有马群的好朋友--凶猛的大狗们，它们一见到狼就会死掐，主人和朋友们都会来救它们的...........<br />
<br />
<font color="red"><br />
.................被踢烂下身，踢下马的狼，大多是母狼。它们比公狼体轻，完全靠自己的体重的坠挂，难以撕开马的肚皮，只有冒死借马力。母狼们真是豁出命了，个个复仇心切、视死如归。它们冒着被马蹄豁开肚皮、胸腑、肝胆和乳腺的危险，宁肯与马群同归与尽。 <br />
<br />
一条被马蹄踢破腹部，踢下了马的饿疯了的公狼，呲牙咧嘴地蜷缩在雪地上嗥叫，可它还是拼命地用两条前腿挣扎着，爬向倒地未死的马，撕咬生吞那匹囫囵个的大马，绝不放弃最后一次机会。只要它的嘴还在、牙还在，它就不管自己有没有肚子，照吞不误。鲜活的马肉被狼大口咽下，没有肚皮容量限制的狼，一定是世界上最贪心、胃口最大的狼，也一定是一次吞下最多马肉的狼。这是狼在临死之前最痛快最惨烈的最后一次晚餐。 <br />
<br />
而那些被狼从肚侧大剖腹的马，本来就是大腹便便的饱马，胃包里装满了草原春天的第一茬青草和上年的秋草，饱胀而饱含水份，下坠份量很重。被撑薄的马肚皮一旦被狼牙豁开，巨大的胃包和肥柔的马肠就呼噜一下滑坠到雪地上。仍在惯性飞奔的两条马后腿，跟上来就是狠狠的几蹄，踏破了自己的胃囊，缠住了自己的肚肠。刹那间，胃包崩裂，胃食飞溅，柔肠寸断。惊吓过度的马仍在奔跑，后蹄把腹腔中的胃袋胃管食道肝胆统统踩绕在蹄下，最后把胸腔中的气管心脏肺叶也一起踩拽出来。大马可能是踩破了自己的肝胆，胆破致死；也可能是踩碎了自己的心脏，心碎而死；或着是踩扁了自己的肺，窒息而亡。狼的自杀是极其残忍痛楚的，因此狼也就不会让它的陪命者死得痛快。狼就是用这种方式让马也陪它一同尝尝自杀的滋味。马虽然是被狼他杀的，但马也是半自杀的。马死得更痛苦、更冤屈、也更悲惨。 <br />
