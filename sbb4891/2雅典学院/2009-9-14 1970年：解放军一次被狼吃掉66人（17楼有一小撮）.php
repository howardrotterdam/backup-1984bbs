　　“说狼聪明，是因狼非常机警，狼与狼之间的配合默契。不管做任何事情，它们总能依靠团体的力量去完成，而且各个成员之间分工明确。”桑加还以巴特尔家遭遇的那群狼举例：“那天狼应该是凌晨四点多钟到达的，这个时间是人睡觉最沉的时候。这一群狼至少8只以上，它们先是观察地形，设好埋伏，然后由头狼带领几只狼从一米多高的羊圈墙跳进去。接着狼群驱赶羊群，一次次冲击圈门，直到把圈门挤坏。这个时候，埋伏着的狼立即接应，把惊散的羊群向山上赶。巴特尔家的羊在3公里处死得最多，说明狼就是在那个地方进行集中屠戮的。”<br />
