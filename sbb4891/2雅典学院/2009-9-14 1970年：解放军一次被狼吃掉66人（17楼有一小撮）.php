													<h2>1970年：解放军一次被狼吃掉66人（17楼有一小撮）</h2>
						
						
																											<div id="postmessage_240230" class="t_msgfont">weixia 2009-9-13&nbsp; &nbsp;Topyl.com - 中国受众传媒网 <br />
<br />
<br />
<br />
<br />
&nbsp; &nbsp; 冯远理先生的《那些本不该消失的生命》，发表于2005年第5期《雨花》杂志上，但我至今才读到。读完文章，我的心在紧缩着，除了震惊，一种无以名状的悲愤在笼罩着我。&nbsp; &nbsp; 不是一个人，几个人，是66个人啊！整整66条生命活活葬送在狼口中。<br />
<br />
&nbsp; &nbsp; 穿过几十年的时光隧道，青藏高原上的那个夜晚，仍让我不寒而栗。我不敢抬头，我怕我的目光投过去便僵住在那个夜晚；我不敢张耳，我怕我的耳畔从此只有令人毛骨悚然的悲嚎和撕咬……<br />
<br />
&nbsp; &nbsp; “1970年3月，一支由来自江浙两省的200多名新兵组成的部队，走出青海贵德县，他们要西渡黄河进行野营拉练。这一天，部队准备在一片叫做老虎坡的荒无人烟的山岭附近宿营。离老虎坡还有三里地时，天已经快黑了，走了一天的战士们已经筋疲力尽。”<br />
<br />
&nbsp; &nbsp; 这段描述有四个因素请注意：<br />
<br />
&nbsp; &nbsp; 一 新兵拉练。<br />
&nbsp; &nbsp; 二 西渡黄河。<br />
&nbsp; &nbsp; 三 筋疲力尽。<br />
&nbsp; &nbsp; 四 附近宿营。<br />
<br />
&nbsp; &nbsp; 纵观悲剧始终，我们需要对这几个因素进行思考。&nbsp; &nbsp;<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;<br />
&nbsp; &nbsp;“这时，一个战士发现对面山坡上有几只狼，在悄悄地尾随着队伍前进。随着天色渐渐的变暗，狼的数量也不断增多。不时有几只狼仰天嗥叫，恐怖的叫声在山间回响，显然是在招呼它们的同伴。不知不觉间狼的数量已经增加到四五十只，并且还在不断地增加中。”<br />
<br />
&nbsp; &nbsp; 这时，向导――一个当地出生的牧民，快步跑到连长跟前，问战士们枪里有多少子弹。连长告诉他，这些都是新兵，从来没有打过枪，枪里一发子弹也没有。听了这话，向导的脸色大变。他说，这样走下去，只怕凶多吉少，这些狼能把方圆几里以内的狼都招过来，他因而建议：改变路线，西渡黄河，因为狼是以河为界的，大河东西两岸的狼各有自己的势力范围，互不来往。”（冯远理《那些本不该消失的生命》）<br />
<br />
&nbsp; &nbsp; 在那个荒诞的年代，一切突出政治，全国人民学解放军，野营拉练还能让几只狼吓住了。这里一个致命的问题被忽视了，既枪里]子弹，这也是文章所强调的。但枪里]子弹，“枪刺”应该还有吧？冯文没提这个问题。尽管那个年代入伍的新兵大都红卫兵过，但红卫兵造反牛鬼蛇神个个好样，面对几十只上百只扑过来的狼群，即使枪上有刺刀恐怕身子已经软了，因为他们走了一天了。<br />
<br />
&nbsp; &nbsp; 现在，狼群还没扑过来，这些雪域高原上的精灵还在聚集力量，如果这时按向导的建议：改变路线，立即西渡黄河，一切都还来得及。你拉练不就是要西渡黄河，今晚在哪宿营并不是目的。<br />
<br />
&nbsp; &nbsp; 然而，短暂的研究后，“连指导员坚决不同意现在西渡黄河，他的理由是，革命战士就应该一不怕苦、二不怕死，逢山开路，遇河架桥，怎能被几只狼吓倒；天黑之前必须以急行军的速度赶到老虎坡。”<br />
<br />
&nbsp; &nbsp; 连指导员是干什么的？支部建在连上的党的负责人。他的理由无人敢反对。<br />
<br />
&nbsp; &nbsp; 悲剧就要发生了! <br />
<br />
&nbsp; &nbsp; 这只筋疲力尽的队伍又站了起来,而这时,一只又一只狼正潜爪在这些军人附近, 一只又一只狼正风一般扑向这里。<br />
<br />
&nbsp; &nbsp; 唉！老虎坡不就是个临时宿营地吗？你以急行军的速度或不以急行军的速度赶到那儿，有什么重要意义吗？但定了就是定了，军令如山，那就再急行军吧。<br />
<br />
&nbsp; &nbsp; 然而，和红军长征不同的是：几百只狼挡住了去路。这些狼可不是什么桂系、川军，和老蒋玩心眼。现在它们已经完成了主力集结，就等着打歼灭战了。<br />
<br />
&nbsp; &nbsp; “队伍被拦，只好再走山口小路，而这正是狼们所要达到的目的。”<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;如果这时连指导员能再问问当地向导还有无办法，也许会损失小一些。可是，革命的连指导员M脑子“一不怕苦、二不怕死。”别的是咸淡不进。<br />
<br />
&nbsp; &nbsp; “就在一排走出山口后，三路狼一下就完成对200多人的包围，一路截断山口的出口，一路堵在山口的入口，第三路完成了对一排的包围。这样就把一排和二、三、四排完全隔离，为把一排吃掉创造了条件。事后证明，狼的部署完全成功。在二、三、四排和狼僵持的同时，一排62名战士除4人尸体是完整的外，其余都被狼啃得惨不忍睹，破碎的内脏到处都是，很多人只剩下一副骨架。空气中弥漫着血腥的气味。连同向导和二排的4名官兵在内，共有67人遇难。”<br />
<br />
&nbsp; &nbsp; 就这样，66名军人和一名向导被狼吃了。那血腥的场面你再没有想像力也会想到该是何等的惨不忍睹，看过“动物世界”的更不必说了。<br />
<br />
&nbsp; &nbsp; 和平时期66名军人竟然会被狼吃掉,无论如何也是一件匪夷所思的事。他们为什么会被狼吃掉?他们是被狼吃掉的吗?<br />
<br />
&nbsp; &nbsp; 笔者曾愤怒批判过极左的“一不怕苦、二不怕死”所导致的一系列悲剧。在那个荒诞的年代，人的生命是多么微不足道。不知后来是怎么对这66名年青军人的父母交待的? 他们知道儿子送上队伍后让狼给吃了吗?<br />
<br />
&nbsp; &nbsp; 如果66名军人中有我们“一不怕苦、二不怕死”的连首长尤其是那个指导员，似乎还好些，毕竟主张“一不怕苦、二不怕死”的人身先士卒了。可是，首长们一个都]死，不仅]死，连长后来还团长了。这就又一次印证了笔者在拙文《〈愚公移山〉批判》中说的：“‘一不怕苦、二不怕死’的实质是让老百姓不怕死。”<br />
<br />
&nbsp; &nbsp; 1970年：解放军一次被狼吃掉66人，该是一件多么令人震惊的大事，然而，事情发生近40年后我们竟然才知道！<br />
<br />
&nbsp; &nbsp; 这件事如果发生在今天，会掩藏得住吗？</div>

							
							
							
							
