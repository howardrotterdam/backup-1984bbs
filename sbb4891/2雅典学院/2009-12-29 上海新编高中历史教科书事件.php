{17}批评者有不少批评意见是针对这些课文插件的，有必要作一说明。这些小插件的设计通常是课堂教学知识延伸，用于启发学生思考、提高学生学习兴趣。以批评者提到的握手礼与拱手礼为例，该课“各抒己见”插件的提问是：“不少学者认为，‘拱手致意’这一中国式见面礼不仅庄重、谦诚，还蕴含着丰富的传统文化精神，又具有卫生、简便等特点，应该以此取代握手礼，你认为呢？”近代西方礼节传入中国以后，到二十世纪下半叶以后，中国传统的拱手礼被完全淘汰，设问的主旨在于通过礼节变化这一特例，通过教师的组织，提示学生关注和思考在西方文明大潮的冲击下，如何保存和发扬中华民族优良传统的问题，这显然并不是像批评者所认为的那样是无关紧要的小事。这些充满生活情趣而蕴含丰富历史意义的小插件，通常扣紧学生生活中的现实，以之反观历史，可以提高学生对于人类文明发展的现实感，从而提高学生学习历史的兴趣。这是上海新编历史教科书对以学生为本理念在教材编写上的具体体现。<br />
{18}像衣食住行、人口增长、生产方式等许多涉及人类历史发展的重要内容，“历史人物”和“历史事件”的概念难以涵盖，所以我特别使用“历史事项”这一概念。<br />
{19}梁启超：“中国史叙论”，《饮冰室文集》之六，第1页。<br />
{20}需要指出的是，重视社会、经济等重大历史事项的发展，是新史学发展的贡献，但忽略个人、事件的作用，又是它的一个缺陷。在目前历史学还没有成熟的解决方案之前，上海历史教科书的处理是折衷的办法，即在初一介绍一些重大历史事件和人物，高一叙述重大历史事项。这样的处理是历史学发展的现状所决定的。问题在于，我们是不是因为新史学发展的这种局限而因噎废食，依旧完全采取以人、事为主体的叙史方式呢？<br />
