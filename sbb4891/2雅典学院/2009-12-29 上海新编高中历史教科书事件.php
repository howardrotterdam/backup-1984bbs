													<h2>上海新编高中历史教科书事件</h2>
						
						
																											<div id="postmessage_403004" class="t_msgfont">帖主提示：上海新编高中历史教科书事件。由教育部召集北京的七位历史学家召开座谈会，<strong>会后作出了“该教科书离开马克思主义唯物史观”、“背离了我们党关于历史教育根本任务的要求，会造成思想混乱，进而危及政治安定”等定性结论</strong>，这套教科书推行一年即停止使用。洗脑，还是得从娃娃抓起。<br />
下文来源：<a href="http://www.opentimes.cn/to/200901/144.htm" target="_blank">http://www.opentimes.cn/to/200901/144.htm</a><br />
<br />
关于上海历史教科书问题<br />
<br />
――对北京几位历史学家批评的回应<br />
<br />
[文 / 周育民]<br />
<br />
　　上海新编高中历史教科书推行一年即告停用，这是建国以来教育界发生的一起极为严重的事件。这一事件的发生，牵涉到许多人事，但深层的原因是历史观念、教育观念的冲突。在这方面，教育部高校社会科学中心在组织几位历史学家召开座谈会基础上形成的六份《社会科学情况反映》，无疑对政府的决策起了重要作用。<br />
<br />
　　关于上海新编历史教科书问题，自2006年9月以来，争议激烈，至2007年9月宣布新编高一历史教科书停用，但仍余波不断。要了解本文的缘起，有必要先交代一下上海新编历史教科书事件的大致经过。<br />
<br />
　　1998年起，上海开始实行第二期课程改革，集中了上海部分大学和中学各学科的骨干力量，分别制定了行动纲领。2001年底，上海师范大学历史系根据上海市教委制定的历史课程改革行动纲领竞标，取得编写权，由系主任苏智良教授担任主编，组成编写组，根据《课程标准》进行紧张的编写工作。2003年开始，新课本在上海近百所中学陆续推出试用，与中学师生不断互动，不断修改，至2005年8月，九册新教材试验本全部编写完成。随后，上海市教委根据各学科课本的编写和试用情况，决定于2006年9月新学期开始，上海地区全部试用二期课改的新课本。就在这年9月1日，美国记者周看（Joe Kahn）在《纽约时报》发表了“毛泽东到哪里去了”一文，根据上海高一历史教材中只有一次提到毛泽东、增加了经济等内容，认为上海历史教科书的历史观发生了重大变化。有些媒体和个人更以“比尔&#8226;盖茨取代毛泽东”、“淡化革命史”等文字指斥上海二期课改历史教科书，于是引发了公众的高度关注。<br />
<br />
　　当年10月，教育部高校社会科学研究中心召集李文海、沙健孙、张海鹏等七位北京历史学家就上海历史教科书问题召开座谈会，会后形成六份《社会科学情况反映》{1}，作出了“该教科书离开马克思主义唯物史观，只讲现象不讲本质”、“指导思想混乱”、“历史虚无主义”、“‘碎化’历史”、“背离了我们党关于历史教育根本任务的要求，会造成思想混乱，进而危及政治安定”等严重的定性结论，要求停用这套教科书。之后，又有以中国史学会名义邀请部分历史学家召开的座谈会，会议简报把上海历史教科书作为中国史学界种种违反历史唯物主义现象的一个典型{2}，上报中央。12月16日，教育部高校社会科学中心主任田心铭在一次会议上透露：“前段时间上海新版高中历史教科书，也存在着一定的问题。教育部很重视，部领导已经有了很明确的态度。”{3}2007年2月，李纪均中将在《磐》公开发表对上海历史教科书的批评。{4}4月，教育部组织中学历史教材审查委员会专家与上海历史教科书课标组和编写组部分人员就上海高中教科书修改方案进行了有益的沟通，虽然大家都具有对上海高中历史课本进行修改完善的共识，但在是否要完全推翻上海高中历史教科书的框架、在2007年9月即推出修改课本等重大问题和工作程序上，各方持有不同意见。到5月份，上海方面个别专家提出完全迎合北京几位历史学家的批评意见，将高中历史教科书推倒重来，并声明在9月即推出新教材是教育部审查委员会专家的意见。市教委不待征求主编苏智良的意见，立即成立新的历史课程标准专家组，重新编写高中课程标准，要求在9月即推出新教材。在改变二期历史课改原定行动纲领目标的情况下，苏智良只能辞去高中历史教科书主编的职务。6月，上海市教委责成华东师范大学历史系编写新的高中历史教科书，在9月开学时推出。<br />
<br />
　　上海新编高中历史教科书推行一年即告停用，这是建国以来教育界发生的一起极为严重的事件。这一事件的发生，牵涉到许多人事，但深层的原因是历史观念、教育观念的冲突。在这方面，教育部高校社会科学中心在组织几位历史学家召开座谈会基础上形成的六份《社会科学情况反映》，无疑对政府的决策起了重要作用。批评者都是笔者尊重的史界前辈，但“吾爱吾师，尤爱真理”。作为上海新编历史教科书的主要编写者之一，我谨就这些《社会科学情况反映》中的批评意见进行回应与辨析，希望引起的是对新世纪中国历史课程改革冷静理性的思考和探索。<br />
<br />
<br />
一、批评意见严重歪曲事实<br />
<br />
　　教育部社科中心所请来批评上海历史教科书的历史学家，在国内都具有崇高的学术地位。既然涉及到学术，那么我们就不能不以科学理性的态度，根据学术规范来审视这些批评意见。《社会科学情况反映》归纳的结论“该教科书离开马克思主义唯物史观，只讲现象不讲本质”的论据是12条具体例证，我看了之后，认为几乎完全不能成立。为了说明问题，我不能不大段引用批评意见的本文和教科书的内容。<br />
<br />
　　1. 批评称：“人类进入文明社会的重要标志是国家的产生，按照马克思主义的观点，国家是阶级矛盾不可调和的产物，国家又和社会形态分不开。但该教科书不讲阶级、阶级关系、阶级斗争，不讲社会制度，不讲社会形态的更迭。文明的历程与社会发展历程是分不开的，离开阶级关系，离开社会形态，孤立地、抽象地讲文明，是讲不清楚的。”<br />
<br />
　　但教材原文清楚地写着：“在军事民主时代，随着战争规模越来越大，军事首领的权力随之增大，职位逐渐世袭化，破坏了军事民主制度。氏族或部落酋长成了世袭的贵族，成为统治阶级。他们利用自己的职权，把战争中获取的大量财富和奴隶攫为己有，甚至压迫和奴役本氏族或部落的成员。这样，氏族社会便走到了尽头，最终为国家所取代。”{5}（上册第13页）“国家的产生是人类跨入文明社会的重要标志。国家机器的建立一方面是社会进步发展的需要，另一方面，也是阶级压迫的产物。”（上册第15页）这里，贵族与奴隶两个阶级的概念出现了，国家是阶级压迫的产物也指出来了。这是不是“只讲现象不讲本质”？是不是“孤立地、抽象地讲文明”？<br />
<br />
　　2. 批评称：“法律是统治阶级意志的表现，是生产关系和所有制关系的产物。但该教科书在讲法律、法系时，连阶级这个词都没有提到。在讲中华法系所谓‘以礼入法、礼刑结合’思想的时候说，礼是‘调整人们之间社会关系的行为准则’。这就抽掉了礼的本质内容，因为礼在中国封建社会是区别上下尊卑、等级贵贱，维护封建统治秩序的”。<br />
<br />
　　关于法律的起源，教材原文的叙述是这样的：“早期成文法典大多由统治者以神的名义颁布，实行严格的等级制，通过严刑竣法来维护统治阶级的利益。”（上册第93页）关于中华法系，教材是这样表述的：“礼最初是祭祀的仪式，后来逐渐演变成为调整人们之间社会关系的行为准则。刑作为阶级统治的工具，一开始便以暴力镇压的面貌出现，如商朝的‘炮烙之刑’等。从西汉中期开始，儒家‘礼’的原则和规范不断地被吸收到法律制度中，到了唐朝，完全按照礼所规定的社会等级和宗法制度来制定法律。以后各代，又沿袭了唐代的立法原则，以礼的精神作为定罪量刑的标准。例如，皇亲国戚、功臣故旧如犯死罪，可以奏请皇帝免除；谋逆之罪株连亲属；以下犯上者加等治罪等。”（上册第95-96页）“以礼入法、礼刑结合”的等级制特点和阶级性质都已经指出来了，这是不是“只讲现象不讲本质”？<br />
<br />
　　上述两段批评的对象，竟然完全是子虚乌有！<br />
<br />
　　3. 批评称：“教科书在讲工业社会时说，‘机器工业的巨大生产力丰富了社会财富，使发达国家的大多数人口首先摆脱了贫困’。‘机器工业的巨大生产力丰富了社会财富’这个判断是对的，但不加分析地说发达国家大多数人口首先摆脱贫困是由于机器工业的巨大生产力，这就明显地歪曲了历史。我们知道，发达国家的财富不仅由于机器工业的巨大生产力，而且由于其殖民扩张，由于其对不发达国家的掠夺和剥削，第一次国际大分工是如此，现在的第二次国际大分工也是如此。”<br />
<br />
　　如果批评者继续读下去的话，一定会看到教材中的这段文字：“工业革命浪潮使人类获得巨大进步的同时，也带来诸多新社会矛盾。首先，工业生产把工人变成了机器的附庸。其次，在资本主义工业化生产的条件下，由于供需等矛盾而导致种种经济危机现象的发生。再次，掌握先进生产方式的西方工业国家为了开拓市场、获取原料，压迫和掠夺殖民地国家，并把人类拖进了规模空前的世界大战。最后，工业生产大量消耗地球上不可再生资源，造成资源枯竭，并严重破坏了自然环境。”（下册第9页）教材在肯定工业化对于社会变迁、政治进步和文化发展的积极历史作用的同时，也指出了工人成为机器的附庸、资本主义发达国家掠夺和压迫殖民地、造成资源枯竭、环境污染的弊病。对教科书中这样力图全面评价工业革命的叙述，可以提出商榷意见，但怎么能随便扣上“明确地歪曲了历史”的帽子？<br />
<br />
　　4. 批评称：“教科书在讲‘中国与现代科学技术革命’时说，‘中国的工业化起步于19世纪60年代，由于统治者的腐败和长期战乱，步履维艰，进展缓慢，东、西部地区发展很不平衡’。这里只字不提西方列强的侵略和剥削，只字不提不平等条约强加给中国人民的屈辱。正是这些侵略和不平等条约延缓了中国历史发展的步伐。”<br />
<br />
　　首先需要指出的是，这段题为“中国与现代科学技术革命”的文字不是课本的正文，而是每课结束时以“史海拾贝”的形式给学生的补充知识，每篇三五百字左右。好在文字不长，我照录如下：<br />
<br />
　　“中国的工业化起步于19世纪60年代，由于统治者的腐败和长期战乱，步履艰难，进展缓慢，东、西部发展很不平衡。20世纪80年代中国又面临着现代科学技术革命的挑战。中国政府实施以信息化带动工业化和西部大开发等重大战略决策，以实现经济、技术和社会发展的飞跃。我国在生物技术、空间技术等方面处于国际先进水平，也是信息技术普及化速度最快的国家之一。中国网民人数1997年仅62万，2002年底已接近6000万，仅次于美国。2002年，中国信息产业产值已达1.5万亿元，成为我国第一大产业，名列世界第三；服务业产值占国内生产总值的三分之一以上。但是，由于制造业基础薄弱和高科技人才缺乏，中国在现代核心科学技术以及与之相配套的精密仪器制造方面，仍然主要依赖发达国家。中国攀登现代科学技术高峰的征程，任重而道远。中国改革开放的成功，将在人类文明发展史上写下厚重的一笔。”（下册第15页）<br />
<br />
　　明眼人不难看出，这段文字的重点是讲述中国与现代科学技术革命，教育学生要努力攀登现代科学技术高峰，而不是分析中国近代工业化落后的原因。第一句话只是一个引子，不是重点。“长期战乱”四个字，自然也包括了帝国主义侵略战争。<br />
<br />
　　5. 批评称：“教科书在讲战略战术时说，‘到90年代，美国开始组建“数字化部队”，并制造了相应的武器装备。2003年，美国打击伊拉克的战争中，大量运用精确制导的空中打击，摧毁了伊拉克的战略设施。’这种描述不仅掩盖了美国不顾国际社会和联合国反对，悍然发动伊拉克战争的侵略本质，而且起了炫耀美国武器装备的作用。”<br />
<br />
　　这又是一个不负责任的批评。该课的重点是讲信息时代战争的特点，并不涉及战争的正义性和非正义性问题。关于这个问题，上海版高中历史教材的视点和教学思路其实是十分清晰的，我在下文会涉及到。美国在运用信息技术于战争方面先走了一步，这是历史事实。把这一史实告诉学生，被无中生有地说成是炫耀美国武器装备，批评者是要让今天的青年一代像当年义和团那样对于先进武器的巨大杀伤力盲然无知吗？<br />
<br />
　　我相信，只有了解世界最新军事技术发展的青年一代，才能认真地应付未来的反侵略战争。顺便提一下，李纪均将军对上海历史教科书作了十分尖锐和过分情绪化的批评，但遗憾的是，他看到的只是关于批评上海历史教科书的报道，而不是教科书原书。上海高一历史教科书“前所未有”地比较系统地介绍了武器、战略战术和战争法规的演变及其与生产方式变革的关系，这是任何一个具体战例都难以涵盖的知识。我们认为，让学生宏观地把握军事发展的历史，更有利于学生理解战争方式、战争谋略和战争性质，树立合乎现代文明准则的战争观。如果说这是“淡化战争”，从何说起？<br />
<br />
　　6. 批评称：“教科书在讲战争规则中的宣战和媾和时说，‘19世纪中叶以后，国际法学界着手研究战争法规，并且形成了开战前应该公开宣战的共识’。这种说法是不符合史实的。19世纪中叶后，帝国主义国家一次又一次对中国发动侵略战争，并不需要什么宣战，也没有什么合法的理由。直到本世纪，美国对伊拉克的侵略战争也没有什么站得住的理由。抽象地讲这些东西对学生是有害的。”<br />
<br />
　　这段批评如果与上段批评出于同一学者之口，那么，我在这里作一系统回答。<br />
<br />
　　先来看教科书在关于“宣战”的叙述：“19世纪中叶以后，国际法学界着手研究战争法规，并且形成了开战前应该公开宣战的共识，1907年海牙会议签订《战争开始公约》。公约规定，开战前必须有‘说明理由的宣战声明或是有条件宣战的最后通牒。’从此，宣战成为判断战争合法与否的国际法准则。1928年在巴黎签署的《非战公约》进一步规定，禁止在国际关系中非法使用暴力和武力威胁。因此，发动侵略战争，不论是否经过宣战，都属于战争罪行。《联合国宪章》更明确禁止成员国非法使用暴力。”（下册第54页）“但是，1894年和1931年日本发动侵华战争，1941年日本偷袭美国珍珠港，1939年德国对波兰和1941年对苏联的进攻都是不宣而战。《联合国宪章》只允许在自卫、安理会授权或采取行动、以及民族解放运动三种情况下使用武力。”（下册55页）<br />
<br />
　　这位专家如果认真读了这段文字，就不应该如此草率地发表批评意见。帝国主义对华发动侵略战争，不宣而战，没有正当理由，这恰恰证明这些侵略战争是违反国际法的战争罪行，至于美英对伊拉克的战争，中国政府的正式表态是“违背了《联合国宪章》和国际法基本准则”，{6}而没有采用批评者“美国对伊拉克的侵略战争”这样的简单定性的表述。授人以鱼，不如授人以渔。上海的中学生读了“战争规则”这一课，学会运用现代战争准则来判别战争的性质，这样做，有益还是有害？再从批评者的手法来看，教科书中的行文主语是“国际法学家”，而在批评对象的主语却是“帝国主义国家”，这种批评手法的名称就叫做“偷换概念”。<br />
<br />
　　7. 批评称：“教科书在讲经济全球化时，只讲‘经济全球化的发展使得世界各国和地区的经济联系空前加强，世界经济增长加快’，没有讲经济全球化的负面影响和实质。我们知道，经济全球化是一把双刃剑，当前的经济全球化是受制于美国霸权主义深刻影响下的全球化，全球政治和经济游戏规则的制定和修改，首先反映的是美国等发达国家的利益。西方主导的经济全球化是造成发达国家和发展中国家之间不平等和世界贫富差距加大的主要原因之一。这个事实应当告诉我们的学生。”<br />
<br />
　　事实上，发展中国家在国际经济关系中仍处于不平等地位的问题，教材中已经明确提到了：“历史上，发展中国家长期作为发达国家的殖民地或半殖民地，造成落后的经济和依附性的经济结构。它们在获得独立以后，并没能改变这种经济地位。”（下册第79页）尽管如此，我个人认为，这位专家的意见表述得更为清晰，是可以在教材修订中加以采纳的，但这并不牵涉到背离历史唯物主义的问题。<br />
<br />
　　8. 批评称：“教科书在讲战后国际格局中的两极争霸时说，‘冷战期间，美苏双方在军事领域针锋相对，剑拔弩张，曾发生朝鲜战争’。且不说把这场战争放在‘冷战’中是否合适，把美国发动侵朝战争用抽象的‘朝鲜战争’一词来表述，这本身就是为美国开脱，也歪曲了中国的抗美援朝的性质。这不仅是史学观点问题，更是政治上的问题。”<br />
<br />
　　教材原文是这样的：“冷战期间，美苏双方在军事领域针锋相对，剑拔弩张，曾发生过朝鲜战争、修建柏林墙、古巴导弹危机等激烈冲突，但却没有演变为两个超级大国的直接军事对抗。”1946年3月，丘吉尔在美国发表“铁幕”演说，被史学界认为是“冷战”开始的信号。第二年3月，杜鲁门的国会咨文标志着美国对苏“冷战”政策的确立。在这种背景下爆发的朝鲜战争，首先是朝鲜南北战争，接着是以美国为首的联合国军干涉，然后才有中国人民志愿军抗美援朝，形成中朝军民联合抗击的局面，因此，朝鲜战争的性质有一个非常清晰的演变过程。就是按照这位专家的个人意见，“朝鲜战争”的一面是美国“侵朝战争”，另一面是中国 “抗美援朝”，如采用“侵朝战争”的名称，你可以说是否定“抗美援朝”的性质；采用“抗美援朝”的名称，你可以说是否定“侵朝战争”的性质。可见，作为这场战争对立面的统一体，还是“朝鲜战争”的定名比较准确。“朝鲜战争”这四个字的提法，也不是上海历史教科书的发明，我引用一下国家教委规划组织的、由吴于廑、齐世荣主编的《世界史&#8226;现代史编》关于朝鲜战争的提法：“1949年底苏联拥有原子弹的消息和1950年中爆发的朝鲜战争更给了美国重新武装西德的借口。朝鲜战争被美国宣传为沿着易北河（欧洲的三八线）进行‘侵略’的先声……。”“1950年6月25日，朝鲜战争爆发。”{7}事实上，国内出版物大量使用“朝鲜战争”这一提法，包括《辞海》，如果按照批评者的逻辑，岂非这些出版物都“歪曲了中国的抗美援朝的性质”，有“政治问题”？推而论之，中国近代史上一些侵略战争的通用名称如“中法战争”、“中日甲午战争”，岂非都歪曲了侵略战争的性质，有“政治问题”？据我所知，有些批评者在自己的论著中也都在使用这些似乎“有政治问题”的名称嘛！<br />
<br />
　　9. 批评称：“教科书在讲中国的社会主义实践和探索时说，‘中国在进行社会主义改造和社会主义建设过程中，最初主要借鉴苏联社会主义模式，取得了伟大成就，但也遭受了严重挫折。1979年以后，中国共产党根据马克思主义实事求是原则，逐渐形成了中国特色社会主义理论’。这种将中国共产党对中国特色社会主义理论的探索历程割裂开来的说法是不符合事实的。”<br />
<br />
　　中国共产党有着把马克思主义普遍真理与中国革命具体实践相结合的优良传统，但在建国初期，由于缺乏社会主义建设的经验，主要是借鉴苏联的经验，如计划经济、集体化农庄等。教科书中的提法是“主要是借鉴苏联的经验”而非“全部”借鉴苏联的经验。胡绳主编的《中国共产党的七十年》中叙述到这段历史的时候是这样表述的：“尽管我们在制定具体的经济政策和工作方法的时候是从中国的具体情况出发的，苏联的社会主义建设经验和经济制度仍然对我国具有重大的榜样作用。”{8}在这一点上，上海历史教科书的表述与《中国共产党的七十年》并没有原则的区别。毛泽东等老一辈无产阶级革命家建国以后作了中国社会主义道路的探索，也发表了不少具有启示意义的观点，但并没有形成建设中国特色社会主义的理论，甚至一度还背离了实事求是的优良传统，最终形成了无产阶级专政下继续革命的错误口号。建设中国特色的社会主义理论是在十一届三中全会以后，党恢复了实事求是的优良传统，在实践中逐渐形成的。中国社会主义道路的探索和形成中国特色社会主义理论，既有联系，又有区别。教科书行文突出的是区别，要求学生掌握的重点是区别。只讲联系，不讲区别，怎么理解十一届三中全会的伟大历史意义，理解邓小平的伟大历史功绩？<br />
<br />
　　10. 批评称：“教科书在讲‘大河文明’时说：‘东方的大江大河孕育了两河流域、印度和中国古老的农耕文明。在河水的滋润下，两岸沃野千里，庄稼茂盛。世代定居的农民，日出而作，日落而息，在星罗棋布的村落中繁衍生息，过着宁静的生活。’把农业文明社会描述成没有斗争和冲突、没有压迫的世外桃源，是对学生极大的欺骗！”<br />
<br />
　　这段批评的引文来自“大河文明”一课的导语，紧接这段引文后面的是一个问题：“为什么在这些大河流域的广袤土地上，会崛起一个个中央集权的国家？这个问题吸引了许多历史学家的浓厚兴趣和不断探索。”可见设计此课的重点是讨论村社与中央集权国家兴起的关系。<br />
<br />
　　我们要问，古代村落中的农民日出而作，日落而息，繁衍生息，过着宁静的生活，难道不是他们生活的常态吗？承认这种生活常态，就一定是否定阶级斗争吗？把农民的这种生活状态说成是没有斗争和冲突的“世外桃源”，这只是批评者自己的想象，而非课文本身的内容。该课最后指出：“为了维持庞大的国家机器和建造公共工程，统治者需要向民间征发赋役。这些赋役主要由占人口绝大分部的农民负担。分散的小农有时无法承受国家的横征暴敛，或者逃往他乡，或者流离失所，或者铤而走险，揭竿而起。”编写者哪里有把农民生活描写为“没有斗争和冲突的‘世外桃源’”的意思？这种批评手法，可以称之为“偷梁换柱”。<br />
<br />
　　11. 批评称：“教科书还不加分析地赞美西方的社会制度，比如在讲三权分立时，详细介绍了西方三权分立的源流、近代三权分立的制度、议会‘三读’制度、国会听证会等，引述孟德斯鸠的话说，‘当立法权和行政权集中在同一个人或同一个机关之手，自由也就不存在了’，‘如果司法权不同立法权和行政权分立，自由也就不存在了’，并总结说：‘现在，不少国家根据自己的国情，积极吸收三权分立制度中的合理因素，努力建成高效、廉洁的国家政治体制。’这种不加分析的介绍看似 ‘客观’，实际上掩盖了资本主义政治制度的实质，对学生是有害的。”<br />
<br />
　　“三权分立”是资产阶级反对君主专制斗争过程中形成的重要政治理论和成果。政治权力相互制衡的“三权分立”理论和制度本身并不能决定其自身的阶级性，阶级性是运用这个制度的统治阶级所决定的。课文明确提到：“随着资本主义文明的发展，资产阶级需要利用议会来进一步制约国王，为自己的利益服务。”（下册第64页）西方权力制衡政治制度的阶级性质应该讲清楚了，怎么能说是“掩盖了资本主义政治制度的实质”？这个批评的对象还是“子虚乌有”。<br />
<br />
　　12. 批评称：“在讲中产阶级时说，‘中产阶级人数的多少，反映了社会的富裕程度’。‘20世纪末，这个人群占美国总人口的80%，巨富和贫困者只占社会的极少数’。这实际上是说，美国已经基本实现了共同富裕目标。可想而知，学了这样的教科书，青年学生会得出什么样的结论。”<br />
<br />
　　严肃的批评应该是质疑教科书用的材料是不是事实？如果是事实，如实告诉学生，这不应该算什么错。不敢承认事实，这不是历史唯物主义者实事求是的态度。见到美国中产阶级占到大多数，就推论说基本实现了共同富裕目标，强加给教科书的编写者，这是批评者自己的推论。共同富裕是社会主义国家所特有的政治目标，批评者把教科书中没有的意思强加上去，然后加以批驳，这又是在“偷梁换柱”！。<br />
<br />
　　综上所述，12条批评意见中，除了少数有价值的建设性批评意见外，有相当一部分是断章取义、无中生有的无理指责，严重地歪曲了事实。有些批评意见，甚至采取偷换概念、偷梁换柱的不正当手法，暴露的不是教科书的问题，而是批评者自身的问题。根据这样一些论据，无论如何也归纳不出“上海版历史教科书离开马克思主义唯物史观，只讲现象不讲本质”的结论。需要指出的是，上述12条例证中，基本上与唯物史观本身没有多少关系，根本上升不到背离历史唯物主义的高度。<br />
<br />
　　为了强调上海高中历史教科书背离马克思主义，批评者竟然说“教科书高一两册中却没有介绍马克思主义的内容，这不能不说是一大败笔。”第二册在第22课明明介绍了马克思恩格斯创立科学社会主义的历史功绩，指出了工人阶级是资本主义的掘墓人，并摘录了《共产党宣言》的警语、配有马克思恩格斯的画像。《社会科学情况反映》竟然不怕他人检验，把这样的批评也写了上去，的确是“一大败笔”。<br />
<br />
　　审视上述批评意见以后，我只能得出――恕我冒昧――“歪曲事实、不合逻辑”的结论。至于批评意见的整理者竟然能从这些意见归纳出上海历史教科书背离历史唯物主义的结论，这就连起码的形式逻辑规则都不顾了。<br />
<br />
<br />
二、唯物史观的科学理性精神必须坚持<br />
<br />
　　《社会科学情况反映》口口声声说教科书的编写要以历史唯物主义为指导，从上面的批评意见来看，它所标榜的“历史唯物主义”竟然可以不顾事实、不顾逻辑，这简直是对马克思主义历史学的嘲弄和侮辱。<br />
<br />
　　马克思主义唯物史观是马克思、恩格斯在批判黑格尔唯心史观的过程中形成的。他们在晚年也曾对青年时代的一些具体观点进行了修正。马克思恩格斯去世以后，他们的学生继续对唯物史观进行了进一步的探讨。因此，唯物史观是一个开放的、不断发展的知识系统，需要我们按照马克思主义的科学理性精神继续研究和发展，提倡学术争鸣。过去我国通行的唯物史观教科书基本上是沿用了苏联的教科书模式，吴英、庞卓恒先生斥为是“苏联传过来的那种充满教条主义和旧唯物论垃圾的‘唯物史观’”{9}，言词虽然激烈，但反映了国内学术界对唯物史观的新追求、新探索。因此，对于上海历史教科书是不是违背历史唯物主义，应该根据中国唯物史观的研究现状和科学理性精神进行恰如其分的评价。<br />
<br />
　　唯物史观的基本观点是人们的社会存在决定人们的意识。社会存在包括生产方式、地理环境、人口等基本要素。教科书根据这个观点，在叙述人类进入文明社会的三大标志（金属工具、文字和国家）时，将金属工具的出现作为首要标志。在“文明与地理环境”单元中分析了地理环境对于人类早期文明的重大影响。在“婚姻与家族”一课中阐述了人口生产方式与社会结构的关系。<br />
<br />
　　唯物史观坚持用阶级斗争的观点来分析人类文明的历史。教科书在专题叙述中也专列了“等级与阶级”一课，分析了阶级的起源；说明与近代社会不同，在传统社会中，阶级划分是通过等级制的形式出现的；随着资本主义的发展，社会日益分裂为资产阶级和无产阶级两大阶级，它们的斗争，深刻地影响着人类的历史进程。这样的处理，既避免了以往把阶级斗争庸俗化、简单化的倾向，又体现了阶级划分、阶级斗争的形式本身也有一个历史的发展过程。在涉及人类文明发展的许多重大事项如国家起源、生产力变革、经济变迁、社会结构、法律制度乃至衣食住行等方面的阶级烙印，教科书在行文中尽可能地给予了阐述。<br />
<br />
　　唯物史观认为，“人们首先必须吃、喝、住、穿，然后才能从事政治、科学、艺术、宗教等等”{10}，因此，物质生活资料的生产是一个民族、一个时代发展的基础。在以往，千千万万普通人的日常生活被排斥在历史课本之外，这不符合唯物史观。马克思恩格斯在“德意志意识形态”指出：“我们首先应当确定一切人类生存的第一前提也就是一切历史的第一前提，这个前提就是：人们为了能够‘创造历史’，必须能够生活。但是为了生活，首先就需要衣、食、住以及其他东西。因此，第一个历史活动就是生产满足这些需要的资料，即生产物质生活本身。”{11}考虑到过去历史教科书在这方面的不足，上海新编历史教科书在“社会生活”单元中，增加了服饰、饮食、居住、交通等内容。这方面的内容引起一部分公众的惊异，恰恰说明我国在唯物史观教育方面的缺失。就是在叙述这些内容时，教科书也注意到它们在阶级社会中的若干历史特点，比如，指出了服饰维持身份等级秩序和明确职业分工的作用。由于是新增的内容，这些叙述难免有许多不足之处，需要在实践中不断修改完善，但编写者贯彻唯物史观的努力依然是清晰可见的。<br />
<br />
　　五种社会形态，是传统唯物史观一个十分重要的范畴。关于五种社会形态的理论，马克思主义阵营内部存在着比较大的分歧，近年来，理论界日益重视马克思提出的“三阶段”或“三形态”之说。{12}如何在中学课本中处理好这个问题，既要考虑到马克思主义理论本身的研究状况，也要考虑到中学生的认知水平。例如，教材在分析中国近代社会性质时，初中阶段不出现半殖民地半封建社会这一概念，而直接概括国家主权和领土完整受到破坏、外国资本主义入侵等清晰的史实，而到高中阶段再将之上升为半殖民地半封建社会形态的概念。高一历史教科书通过国家的产生过程，阐述了人类从原始社会过渡到阶级社会的过程。对于第一个阶级社会是奴隶社会还是封建社会的问题，中学教材未作处理。为什么？我引用著名历史学家吴于廑先生的观点来说明这个问题：“亚欧大陆各农耕中心进入阶级社会之后，在世界历史上划分奴隶制阶段和封建制阶段，是一个复杂的问题。从许多专门研究可以看出，古代世界物质财富直接生产者被奴役、被剥削的方式，生产资料的占有制，包括残存的原始公社共有制，不同地区存在着明显的差别。完全丧失自由的奴隶、半自由的处于依附地位的劳动者以及自由劳动者在各自社会经济中所占的比重，各地也不一致。这种情况不仅在通常以公元5世纪为下限的古代世界是这样，5世纪之后，在中古时代封建制下的直接生产者被奴役、被剥削的方式，以及依附农民与自由农民在经济中的各自比重，也是这样。因此，前资本主义的两个阶级社会，即奴隶制社会与封建社会，都很难以某一地区历史实例作为典型，也很难以某一实例所达到的发展阶段作为世界历史上划分两个社会形态的标准。……在目前，这仍然是马克思主义史学必须深入探讨的重要课题。”{13}因此，上海教科书中对古代社会历史中奴隶制和封建制社会形态的概念运用是十分慎重的。我们还不能要求中学生一定要掌握这样一个高深的尚未定论的知识难题。对于工业文明时代出现的资本主义和社会主义两种社会形态，教材认真阐述了资本主义在欧洲取代封建制度的历史进步和弊病、社会主义社会的理想与实践，并指出了社会主义取代资本主义的历史前景。因此，马克思主义关于社会形态由低级走向高级的基本规律在教材中是清楚的。<br />
<br />
　　顺便提一句，在北京的几位历史学家对上海高一历史教科书发表的结论性意见中有一句话：“中学历史教科书只能讲确凿的历史事实和无误的历史结论，讲马克思主义揭示的社会历史发展规律，不是发表个人学术见解、开展百家争鸣、学术探讨的场所。历史学界有争议的学术问题，不能写进中学教材。”且不说这个结论性意见的前提完全不符合马克思主义关于绝对真理与相对真理关系的原理，也不符合史学工作的常识，基本站不住脚{14}，即使按照他们的意见操作，关于奴隶制社会与封建社会在各国历史中是否一定是前后相继的两个形态这个历史学界有争论的问题，也只能是不写进中学历史教材。<br />
<br />
　　根据上述分析，我们看不出教科书有什么背离历史唯物主义的问题。需要审视的，倒是批评者自己的“唯物史观”出了什么问题。我再次引用吴英、庞卓恒先生的文章：“充满教条主义和旧唯物论谬误的‘唯物史观’最为惹人厌恶的一点，就是用教条主义和旧唯物论观点对待阶级斗争和阶级分析，以至出现德高望重的老一辈马克思主义史学家翦伯赞先生痛心疾首地指出的那种戕害历史科学生命的‘见地主就骂，见农民就颂’的死教条。尤其令人痛心疾首的是，那种死教条不仅严重歪曲了历史真相，而且在‘文革’时期与现实中的‘以阶级斗争为纲’和‘越穷越革命’等‘左’的教条相呼应，产生了极其恶劣的影响。我们永远不能忘记这一历史教训。”{15}应当指出，这种教条主义的“唯物史观”虽然余威犹在，但已经受到广大史学工作者的抵制和引起青年学生的普遍反感，必将为思想解放的浪潮所荡涤。作为一名史学工作者，我坚定地相信唯物史观及其科学理性精神一定会战胜形形色色的唯心史观，纠正人们对唯物史观的错误认识，一定会成为青年一代观察人类文明发展历史的有力思想武器。改革开放以来中国历史学的长足进步，对于唯物史观的研究深入发展，证明了唯物史观的强大生命力。{16}<br />
<br />
<br />
三、“碎化”的批评和历史的“碎化”<br />
<br />
　　历史教科书是一个相对完整的知识体系，从中学课程设置来说，整个课程设置更是一个庞大的知识体系。《社会科学情况反映》仅仅就高一历史教科书提出各种各样的批评，然后得出上海历史教科书“碎化”历史、“历史虚无主义”，“背离了我们党关于历史教育根本任务的要求，会造成思想混乱，进而危及政治安定”等等危言耸听的结论，这种论辩手法，就是攻其一点，不及其余，以偏概全，借用批评者的话说，这是“碎化”的批评。<br />
<br />
　　历史教科书当然要贯彻中央领导人关于革命史教育的指示，这一点，上海历史教科书的编写者与批评者并没有分歧。问题在于批评者有什么根据说上海历史教科书取消了革命史教育。上海的新编初中历史教科书下册就是叙述1840年以来帝国主义侵略中国和中华民族争取民族解放和振兴的历史，讲述中国共产党人走农村包围城市的革命道路、领导建设中国特色社会主义的成就，教科书附配的光盘资料内容更加丰富。外界所误传的上海教科书删除了狼牙山五壮士、没有南京大屠杀等内容，实际上都包含在这本历史教科书及其附配光盘中。高三历史教科书中，同样论述了中华民族在十八世纪没有抓住历史机遇而在十九世纪落后挨打，二十世纪在中国共产党领导下争取民族解放、民族振兴的历史。这样一套历史教科书竟被批评者指斥为“背离了我们党关于历史教育根本任务的要求，造成思想混乱，进而危及政治安定”，实在令人难以理解。<br />
<br />
　　一位批评者有一大段的抱怨，集中地反映了批评者关于上海高一历史教科书“碎化”历史的批评：“该教科书名为‘历史’，但实际内容很难说是什么史。它打破了中国史、世界史，古代史、近代史的界限，名为文明史，片断地讲了生产工具，片断地讲了一些社会生活，把历史变成一些社会小常识的杂乱的堆砌，比如讲名片是由中国人发明的，讲拱手礼比握手礼要卫生，让学生讨论到底是拱手礼好还是握手礼好，讲领带是如何流行起来的，讲‘领带小史’比讲十月革命还要详细。但几乎只字不提文明史上的社会关系、阶级斗争、政治生活、文明演进等等，几乎只字不提世界近现代史、中国近现代史的重大事件，没有基本的历史事件和重要的历史人物。像这样没有基本历史内容的历史教科书，究竟是进行历史教学，还是取消历史教学呢？”需要说明的是，批评者所举的例子大多不是课文的正文，而是附带的知识和提示{17}，这些具体问题如何处理和修改，完全可以通过正常的讨论。批评者所关注的一些重大历史事件和人物，在初中历史教科书中已经叙述过了。至于怨声背后所反映的，恰恰是批评者轻视社会生活史的历史观念。<br />
<br />
　　历史专业工作者都知道，人类的历史现象纷繁复杂，要将这些现象按照历史本身的发展整合起来，现行所有的历史体裁都有各自的局限。中国古代历史学家发明了编年史、纪传体、纪事本末体等体裁，西方史学家发明了通史体的体裁，都只能片断或局部地反映人类文明历史。按照年代顺序、人物、事件叙述历史，既是这些历史体裁的长处，也存在着轻视地理环境、经济变迁、人类社会生活和精神生活的短处。唯物辩证法关于事物普遍联系的规律在历史学科中的运用，自十九世纪马克思主义诞生以后，虽然有了长足的进步，但依然任重而道远。至于形成融历史事件、历史人物、历史事项{18}于一体的历史新体裁，至少在本世纪初还看不到任何端倪。因此，批评者指斥上海历史教科书存在“碎化”的问题，可以说是整个历史学研究需要克服的通病。这种情况，在其他学科中同样存在。以医学为例，将人体分为循环系统、消化系统、神经系统等等分门别类地进行研究，但人体各种系统相互关系、人体的整体生命运动，至今仍然是医学研究的难题。从科学史的发展来说，分科治学、分类研究，即批评者所谓的“碎化”，是近代科学发展的重要动力和特点。二十世纪许多科学家都意识到这种偏重分科治学的弊病，提倡跨学科研究，但科学整合依然是整个科学界的难题。社会现象远比自然界复杂，而研究古往今来人类文明发展的历史更为艰难。因此，对于历史学科发展的现状，作为历史工作者，我们既不应自卑，也不能狂妄――狂妄到自以为可以向中学生端出一整套没有争议、展现历史整体的教科书来。<br />
<br />
　　上海历史教科书采取三种编排历史内容的方式，是对中学历史教科书体裁的一个新探索。初中历史教科书采用大家所熟悉的通史体裁，高一历史教科书采用的是专题分述的形式，高三历史教科书采用的是从人类文明发展看中国和平兴起的大叙事结构。这样多形式的编排历史内容，从教学来说，在要求学生掌握一些基本历史知识的基础上，重视历史知识的理解、运用而非简单的背诵记忆，使学生学会从人类文明发展史中汲取对自己发展有益、有用的历史知识的方法。以学生发展为本，这是上海二期课改的理念。专题分述，是大学教育中普遍采用的方法，其形式就是专题讲座。用现代科学的术语说，这是分门别类的专题研究；用批评者的贬义词表达，就是“碎化”，从这个意义上说，批评者撰写的一篇篇论文，也是在做“碎化”历史的工作。将某些大学教育中行之有效的教学方法引用到中学来，强化师生间的互动和学生自主学习探索，这是上海历史课改在教学方式改革上的一个尝试。<br />
<br />
　　至于高一历史教科书专题的编排，也并非像批评者所说的那样，不遵循唯物史观，将历史“碎化”得完全杂乱无序。从时序上说，高一上册的时间范围主要以 1500年以前为主，下册以1500年以后为主。从内容编排上说，第一主题叙述人类早期文明诞生的标志及其与地理环境的关系，后者着重讨论受到地理环境制约的生产方式与国家政治制度的关系；第二主题叙述人类的物质生活和社会生活，这是人类社会发展的基础，第三主题叙述作为社会存在反映的人类意识形态，这方面的内容由于下册不再涉及，部分延伸到二十世纪。下册的时间范围侧重于1500年以来人类文明的发展。主题四“文明交融与冲突”是承上启下的内容，农业革命、工业革命和信息革命即所谓“三次浪潮”是人类生产方式最伟大的三次变革，教科书概述了三次浪潮对于人类生产、生活和社会产生的深刻影响；高等教育、博物馆与图书馆的发展是工业革命以来文化教育事业发展的显著特点（广播电影电视因初中历史课本已有介绍，不再重复）；三次浪潮也推动了人类活动空间的拓展、不断改变人类战争的形式和观念，这些内容在通史课中也很难集中展开，需要专题论述。主题五“走向经济全球化时代的文明”主要叙述工业文明时期形成的主要国家形态、经济制度和国际政治经济秩序。主题六“文明的现实与未来”阐述人类社会发展的目标和问题，资源环境问题在叙述农业革命与工业革命时已经涉及，不再重复，而侧重于人权、社会保障和社会主义理想等问题。<br />
<br />
　　这样一套系列专题的框架设计，的确如批评者所说，“前所未有”，但绝不是“与国际接轨”，因为目前各国的历史教科书中没有采用这种框架的先例。有些专家认为框架结构不成熟，没有史学界的共识，这也是事实。问题在于，对这样一个新生事物，我们应该采取什么态度，是因为其不成熟而加以扼杀，还是允许它在实践中不断发展成熟起来？<br />
<br />
　　高一教科书中所涉及到的这些人类文明史发展中的一些重大事项，在通史叙述中很难集中展开，而又是历史教学中必须加以充实、启发学生思考的。在这样一些全人类共同参与的重大历史事项中，个别历史人物活动、个别历史事件影响的重要性显然要退居后台。高一历史教科书试图通过这样一些人类文明发展重大事项的提示，讨论其中的相互关系，为学生运用历史唯物主义观察、理解、探讨人类文明发展历史提供一个更加广阔的历史知识平台。梁启超早在二十世纪初叶指出：“前者史家不过记述人间一二有权力者兴亡隆替之事。虽名为史，实不过一人一家之谱牒。近世史家必探察人间全体之运动进步，即国民全部之经历及其相互之关系。” {19}因此，如果了解二十世纪历史学发展的趋势，根本无须对高一历史教科书没有面面俱到地叙述重要历史事件或历史人物而大惊小怪。{20}<br />
<br />
　　由于上海历史教科书采用了文明史的概念，有些先生提出中学历史不能提文明史概念。一位自己编了世界文明史的学者竟然也公开讲，文明史概念不符合马克思主义，不能用。其实问题不在于文明史这一概念本身，而在于我们如何看待文明史。<br />
<br />
　　毛泽东曾经讲过，一部文明史，就是阶级斗争史。胡锦涛也讲文明史，“一部人类文明史就是人类不断在以往历史的基础上有所发现、有所发明、有所创造、有所前进的历史。”{21}毛泽东、胡锦涛是在不同的历史时期，根据党的中心任务，从不同角度来谈人类文明发展历史的。我们在叙述文明史时，既要讲阶级斗争是人类文明发展的重要动力，也要讲人类有所发现、有所发明、有所创造、有所前进。阶级斗争是我们理解人类文明发展历史的一把钥匙，但并不是人类文明历史的全部内容。戴逸先生说得好：“唯物史观把阶级斗争看作阶级社会前进的动力。马克思主义反复强调，阶级存在于一个统一体内，各阶级之间既有矛盾对立性，又有相互统一性，我们既要看到阶级社会中阶级斗争的存在及其作用，又不能把社会看成仅仅是阶级之间的斗争。把阶级斗争绝对化，以阶级斗争为纲，这又是对马克思主义的曲解。”{22}近几年来，胡锦涛、温家宝等同志在各种场合强调文明的创新，文明的交流，文明的差异，文明的兴衰和区域、民族文明对于人类文明的贡献，强调人类文明史上进取、和谐的一面，这是我们党放弃以阶级斗争为纲，把工作重点转移到经济建设、转移到建设和谐社会上来，在文明史观念上的一个新发展。这种理念体现了党和政府的意志，符合人类文明发展的客观规律，应该在新编历史教科书中体现出来。上海高一历史教科书用专题形式，努力展现人类进入文明社会以后物质资料生产方式、物质社会生活的巨大变迁，由地区走向全球、太空，在传承、交流和冲突中不断发展，为消灭等级差别、阶级差别、为建立公平正义的理想社会不懈努力的历程。站在二十一世纪起点的中国青年应该有这样广阔的历史视野来面对人类社会当前的现实、思考人类发展的未来。<br />
<br />
　　上海版的历史教材因为改革力度较大，肯定有不成熟的地方，需要不断加以改进和完善。但是，由于《社会科学情况反映》对上海历史教科书提出了种种不符事实、上纲上线的严厉批评，影响到教育部、上海市教委领导对上海高中历史教科书问题性质的判断，上海市教委最终采取停用的严厉措施，酿成了改革开放以来中国教育界罕见的重大事件。<br />
<br />
　　现在，上海新编的高中历史教科书虽然夭折了，但这不会改变我们对中国历史课程改革的信心和关切。毕竟，历史上的任何改革都不会一帆风顺，上海新编高中历史课本的夭折也已经成为历史的一部分。全国和上海的历史课程改革仍在继续，我们相信，编写者经过不懈努力和探索，一定能够按照唯物史观科学理性精神，编出一套新的符合党和政府要求、具有时代精神、受到学生欢迎、有益学生发展、吸收历史学科新成果、多侧面反映人类文明进步的历史课本来。<br />
