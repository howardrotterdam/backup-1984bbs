同样属于国内畅销书领域的80后作家，韩寒与郭敬明常常被拿来作比较，两人的恩怨是非传言很多。韩寒澄清自己与郭敬明不是同一类人，而且男女有别；自己除了钱比郭敬明少，其他都比他强；批评郭敬明的杂志的价值观很贱，招90后尤其是城乡结合部的孩子喜欢。<br />
