我和郭敬明男女有别<br />
<br />
南都周刊：总是有人把你和郭敬明相提并论，你们的恩怨是怎么回事？<br />
<br />
韩寒：其实我个人和他没有什么恩怨，我从来没有把他当做和我一类的人，我们完全不一样，没有办法比较。我们都属于畅销书领域的人，年龄也差不多，属于80后这一代名气大的作家，所以很多人将我们拿来对比。但我们性格差异比较大，而且最关键的是我觉得我和他男女有别，没有什么可比性。我们个人审美观也完全不同，他喜欢的东西在我眼里不值一文。我们只有一个共同点，就是我们都养着一条金毛，金毛真是奇怪的动物，能把两个完全不相同的人联系在一起。他喜欢的东西，喜欢去的场所，都是我所不喜欢的，因为可能对他来说，需要时时刻刻确定他是在上海的大都市才能给他安全感，但对我来说，不需要，即使天天在乡下，我也很开心。<br />
<br />
南都周刊：外界有评价说郭敬明会挣钱，韩寒会玩。<br />
<br />
韩寒：他会挣钱我承认，但我不会玩。<br />
<br />
南都周刊：四年前你就想做杂志，但因为郭敬明做了，你为了不想被说成跟风而放弃，现在为什么不怕了？<br />
<br />
韩寒：之前我和朋友说过要做杂志。郭敬明先做了一本杂志，虽然一开始不是很成功，但毕竟先做了，我想我就不要做了。但是现在时机比较成熟了，做杂志的也多了，不止他郭敬明一个。我自己也考虑清楚为什么要做这个杂志了，所以现在也不会怕别人说跟风了。而且，当年我出版了书，难道后面出书的就叫跟风吗？这不是专利产业，只是普通行业，不存在跟风不跟风。虽然做杂志已是夕阳行业，但是夕阳无限好，而且人们的生活需要更多文学。<br />
<br />
南都周刊：你们的杂志有什么不一样？<br />
<br />
韩寒：我做的杂志不是想拯救谁，也没有说要给谁希望，只能说稿费开高点，改善下作者的生活，再就是内容上给大家些开心。事实上，郭敬明提供的道路不能让人开心，他告诉人家爱马仕有多好，但是以他的读者的购买力，可能买本他的杂志都够呛了，你给他灌输这种思想，而且看他书的孩子还小，多是90后，他给他们灌输这样的价值观是不对的。事实上每个人都有不同的价值观，不分对错，但是分贵贱，他灌输的价值观是很贱的价值观。真正的价值观是你有一个理想，可能物质实现得了，可能物质实现不了，但是他给的价值观是最纯粹的物质。这个本身也没有问题，因为每个人都追求物质，但是你要给人提供一个渠道，你要告诉人家怎么样满足，他没有告诉人家这个。好像每个人生出来都是富二代，都不知道哪里来的钱，他没有告诉读者这些东西的获取要靠个人努力。他个人是很努力的，他应该告诉读者我郭敬明很努力，而不是生来就有财富。<br />
<br />
世界由男人主宰女人毁灭<br />
<br />
南都周刊：你们俩见过面吗？<br />
<br />
韩寒：只见过一次，不喜欢。最重要的是对他实在看不顺眼，长得不好看，不在我的审美里。对于他这样一个人的存在，我除了钱比他少外，所有都比他强，如果一个人生全部拿钱衡量，我也没有办法，可能他的东西在90后那里有吸引力，尤其对于城乡结合部的孩子。就好比每个人都有美国梦，把美国的东西带来中国，大家都觉得他很洋气，可能在美国他只是个土鳖。他把上海梦带到了各个城乡结合部，可能，他们觉得很洋气。他是个很努力的人，我很钦佩。实际上，我的杂志和他的有很大的差别，不应该老是去谈论他，但实在是情不自禁地要去说说他。<br />
<br />
<br />
<br />
南都周刊：没上大学对你的成长有什么影响？<br />
<br />
韩寒：我没有接受过大学教育，可是每天和我工作在一起的，我的工程师，电脑师，都是世界最顶尖的。我没有接受过文科教育，可是我写的东西是最好的，远远超过受过高等教育的人。没有念大学给我的影响是，在一个应该泡妞的时间，我没有去大学，后来我想明白了，原来不在大学里面更容易泡到妞。对我来说没上大学没有任何缺憾，我所看到的，想到的，都不会输给大学生，无论清华北大。<br />
<br />
南都周刊：如果有小孩你会让他上大学吗？<br />
<br />
韩寒：看他喜欢什么，喜欢去大学泡妞就去。但是我希望我的小孩是女孩，因为小男孩，如果他不如我我就不高兴，但如果他什么都比我好，我更不爽，那我岂不是个悲剧。女孩子则不同，可高可低，可进可退，行就行，不行还可以是个可爱的小女孩，我很不喜欢女权主义。这个世界的规则是由男性主宰，但我想如果有个按钮能毁灭世界，男的一定不会按这个按钮，一定是女的去按。所以世界是男人主宰，由女人毁灭，算下来是公平的。 (本文来源：南都周刊 )</div>

							
							
							
							
