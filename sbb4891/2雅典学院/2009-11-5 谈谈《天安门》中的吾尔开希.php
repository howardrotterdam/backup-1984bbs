													<h2>谈谈《天安门》中的吾尔开希</h2>
						
						
																											<div id="postmessage_314884" class="t_msgfont">　　　　　　　　　　　谈谈《天安门》中的吾尔开希<br />
<br />
<br />
　　之所以写《谈谈柴玲的录影讲话》，是因为柴玲的更加广为人知和更具争议，尤其是她的“流血”和“求生”。但其实，经过这二十年，如果再有人呼吁“用我们的鲜血和生命来唤起民众”，声称“黎明就要到来了！……一个真正的，人民的，民主的共和国，也就要诞生了！民族存亡，最后的关头，已经到来了”，而以此号召民众的话，估计已经没多少人会响应了。所以，我在上文说：“对‘权力’的反思，要比‘流血’问题更重要。”同时，更是因为在国内网络平台上，“不负责任的无端指责，没有证据的诛心，罔顾事实的妄言”已几乎成了常态。<br />
<br />
　　对那场运动，由于众所周知的原因，我们得不到详尽具体的资料，《天安门》是很少几个比较详细且如实地叙述了运动整个过程的影片。――顺便说，在笔者看到的所有有关这场运动的论著中，另一部值得推介的是《天安门之变―陈小雅》，该书更多的是分析和思考――而对于这样一场唯一一次完全代表了人民心声的民主运动，就其本身，不容不详查；就其对日后民主进程的作用，不容不深究。――也顺便说，76年的四五运动，虽说也代表了当时人民的心声，但那时人民的认识并没有经得住历史的考验――那么，仅仅查究柴玲一个人的表现无论怎么说也是不够的。下面，就对《天安门》中的吾尔开希，谈谈个人看法。<br />
<br />
　　下文中【】所引，均出自《天安门》，大部为吾尔开希的采访谈话，个别为影片中的镜头。为读者查阅的方便，以下按《天安门》中的出现先后为序。“《天安门》影片记录文稿”另帖发布，以晃薹看到影片的朋友们。视频影像包含更多信息，而文字稿有时更利于归纳思考。<br />
<br />
<br />
一、<br />
<br />
　　在新华门“四二零惨案”中：<br />
<br />
　　【吾尔开希在人圈中嘶声地喊：“各位朋友，各位朋友，往后靠一靠。（众人：别挤，别挤！）我是，我是今天凌晨，‘四二零惨案’的目击者、组织者、还有一些其他情况的知情者。我们在那里静坐一直坚持到了早上五点。（众人：好！）在这个时候，他们出动了近千名军警，野蛮地冲击我们的队伍！野蛮地殴打我们的学生，还有北京市的其他各界的朋友。打伤人数可以说不计其数！而且他们对我们的女同学进行了猥亵，污辱！”】<br />
<br />
　　当局出动军警驱散请愿学生，使用了暴力，这是无疑的，说“打伤人数可以说不计其数！”也算不上太大夸张。但如果说军警在这个过程中“对我们的女同学进行了猥亵，污辱！”，一是从未在其他回忆和记录中看到，即便从一般常识推测，应该说没有这个可能。作为国家机器或“党卫军”成员，无知盲从残暴是普遍的，部分人心存阴暗的流氓心理也是存在的；但在光天化日之下，在执行任务之中，公然对女同学进行猥亵侮辱，应该说可能性几乎不存在。<br />
<br />
　　还是侯德健说的：“我们是不是需要用谎言去打击那些说谎的敌人，难道事实还不够有力吗？那么如果我们真的是用了谎言去打击说谎的敌人，那只不过是满足了我们一时的泄恨，发泄的需要而已。那么这个事情是个很危险的事情，因为也许你的谎言会先被揭穿，那么之后的话，你再也没有力去打击你的敌人了。”<br />
<br />
　　在政治斗争中，为“壮丽事业”的目的而“不择手段”，正是我们所反对的。用谎言去打击敌人，不仅有先被揭穿的危险，而且首先丧失了追求民主公正的道德优越。尤其在广场政治中，民众缺乏理性而易于狂躁，民粹倾向更是最大的潜在危险。当然，从整个运动来看，理性非暴力是主导。而越是这样，其中的不协调更令人感到缺憾。<br />
<br />
<br />
二、<br />
<br />
　　对胡耀邦遗体告别仪式中三个学生的“跪谏”：<br />
<br />
　　【学生运动前一阶段叫做“谏议”阶段，“谏”是中国古代纳谏的那个“谏”。在这个谏议阶段中我们有过直谏，有过泪谏，也有过跪谏。就是我们可以跪下来，但还是谏，是一种臣对君的关系，还是一种我们求你们出来跟我们对话。当然三个学生一跪，可以说是跪倒了一个中国政府，使中国政府的形象彻底暴露了。】<br />
<br />
　　三个学生的“跪谏”，很难说代表了学生群体的一致心态，也不可能是通过什么程序作出的决定，不过是当事人一时的情绪所致。事后对此有过不少评论和批评，认为这表明学生观念深处一种封建残留，缺乏公民意识，暗含了传统中对明君清官的期待，而对公民意识和民主秩序尚显模糊。但吾尔开希对此的评价是：“跪倒了一个中国政府”。这种自我感觉过于良好的心态，忽略了更深入的思考，也难以在面对危机的时候有清醒的分析和理性的应对。<br />
<br />
<br />
三、<br />
<br />
　　对5月14日晚十二学者来广场劝说学生：<br />
<br />
　　【知识分子错就错在他们没有把自己到底是什么看清楚。他们在扮演一个政府和学生之间的斡旋人这样一个角色。我们能使政府的代表团和我们这样面对面地坐下来谈判，这是建国四十年来没有的，这是我们作为学生一支独立力量，我们做成了！而到这一天，我们诚挚地请知识分子参加我们的时候，他们到广场上说的一句话是：孩子们……他们觉得你们做得太快了，咱们还是慢慢来。你们最好还是听你们的爸爸妈妈，你们最好还是听你们的老师，听政府的话。我不想说太多，你们有什么资格批评我们？！】<br />
<br />
　　吾尔开希同样错就错在“没有把自己到底是什么看清楚”。他只看到政府和学生面对面坐下来谈判，他不知道这背后是否有民意的取向作为基础；他不知道如果真是仅仅代表了“学生一支独立力量”，政府还会不会和你“面对面地坐下来谈判”。他完全不了解近百年来中国知识分子都曾经做过什么，他甚至不知道文革之后的13年里都发生过什么，甚至忘了前两年发生过的学潮；这次运动以胡耀邦逝世为契机，但他也不了解胡耀邦在学潮、知识分子和中共保守派之间发生过什么。在他眼里，这场运动中知识分子是被学生“诚挚地请”来的，他不认为中国的民主进程离不开知识分子和大众的参与。<br />
<br />
　　最后，他不无狂妄地说：“你们有什么资格批评我们？！”他没有说明的是：批评学生需要什么样的资格，需不需要资格；学生需不需要批评，可以不可以批评。<br />
<br />
　　对照之下，刘晓波反省自己说：“能不能用理性把握住自己的行为，能不能在万众欢腾情况下，知道自己究竟这半斤八两是什么，知道自己的角色是什么，这个东西太难了！”<br />
<br />
　　那么，在吾尔开希心里，自己是怎样的呢？他说：<br />
<br />
　　【大概没有一代人象我们一样嘲讽过国家，嘲讽过政府，嘲讽过领袖；而且没有一代人象我们这一代人那样看到过外面的世界那么美！……】<br />
<br />
　　如果说认为“没有一代人象我们一样嘲讽过国家，嘲讽过政府，嘲讽过领袖”还是对历史的无知的话，那么，说“没有一代人象我们这一代人那样看到过外面的世界那么美！”――我倒是想问：你看到的“外面的世界”是怎样的？对“外面的世界”你到底看到多少，又理解了多少？<br />
<br />
　　在《谈谈柴玲的录影讲话》的《第三，“对谈”》中，我建议朋友们从中看看“柴玲对运动的理解，也可以看出柴玲本人的素质修养”。同时我也说明了：“我并不据此贬低柴玲，但不由得要想：在风云突起之际，在广场政治之中，人们要怎样才能更清醒和理智？尽管我经常说潮流中的弄潮儿往往并不是素质最好的人，但我们还是希望能够将更恰当的人送上潮头。”<br />
<br />
　　在《天安门》中吾尔开希没有这样“对谈”，以回答诸如：对民主的理解；和政府对话的最终目的，对反腐败、反官倒的看法；当代大学生的思想来源，比如甘地或马丁・路德・金的影响。但他也简要说到：<br />
<br />
　　【可以想象一下我们这一代年轻人，我们有过什么？我们没有过我们父母所有的追求，我们也没有过我们兄姐所有过的那些狂热的理想。那么我们所想要的是什么？耐克鞋（Nike），有充裕的时间和自己的女朋友去酒吧，有充裕的自由平等去和别人谈一个问题，能够得到这个社会的尊重。】<br />
<br />
　　父辈的追求和狂热的理想，其中确有必须扬弃的部分，不加以批判的继承，也罢；耐克鞋、酒吧，自由平等、社会尊重，作为正常年轻人想要的，也无可非议。但是，作为运动的领袖，是不是还少点什么？<br />
<br />
　　而这样的人能成为潮流中的弄潮儿，却是合乎常理的，这就是“广场政治”的特点之一。同时，这也恰恰是十分值得警醒的，在日后的民主进程中，或甚至在当今的网络平台上。<br />
<br />
<br />
四、<br />
<br />
　　笔者一再强调“回顾历史，检讨过失，提高自身，迎接未来”，好像很有点恢弘叙事的感觉，其实，我看重的是国内的网络平台，我把现在的网络平台看作是未来民主制度的一个预演。<br />
<br />
　　互联网的出现和发展，无疑对国内民主力量的积蓄和扩展起着最重要的影响。对国内丑恶现象的揭露，民主理念的宣扬，与新左、毛左等意识的论争，以至公民维权等，无一不以互联网为主要阵地。甚至很多著名学者，从开始在论坛上潜水观战，现在也以网络平台作为一种表达的重要方式。同时，网络也是时下中国唯一可以自由言说的空间。所以，这是中国未来的希望之所在，是我们不得不重视和珍惜的地方。但是，网络论坛，由于其开放性和自由性，似乎只能是个“广场”。一个没有严格的规则，可以尽情发泄而不需要负责任的地方，是不是很象一个广场？<br />
<br />
　　二十年前的天安门广场，先是一小批先觉者来到这里，心怀理想和真诚；然后是大批的民众来到这里，充满着热情和奉献。但是，五千年的文化传统和四十年的革命教育，并没有滋养出足够的民主素养。这里是广场，没有组织的约束，没有严格的规则，可以尽情发泄而不需要负责任。在这里，你可以信口开河而无须以事实为据；在这里，民粹的喧嚣更容易获得狂躁而缺乏理性的支持。<br />
<br />
　　二十年后，当我们在互联网上看到未来希望的时候，互联网上的论坛又出现了似曾相识的“广场”：一小批先觉者先来这里耕耘，开辟了自由思想的园地；大批关注社会的思想活跃者，闻风而聚。到现在，却经常可以看到：污言满目，秽语成章，人身攻击和诛心，毫无理性和教养；自以为是，唯我独大，狂妄无知，自我感觉良好……<br />
<br />
　　当然，我不是说论坛上多数如此，但这种现象随处可见。而广大的看客，失望之余不禁发出这样的感叹：要是中国真的实现了民主，会不会也是“酸”的？<br />
<br />
　　不难看出：两者之间，相承的是五千年传统文化和六十年主流教育对我们每一个人思维和行为方式上的渗透。同时，这种渗透还由于其已经融化到我们每一个人血液中，而必然贯穿我们的全部。70后们会说：我们出生在文革后，狼奶是红卫兵的特征，封建传统是文革前的教育。不错，互联网极大地开阔了信息的来源，现在，我们拥有比文革前多得多的思想资源。但，五千年的传统从未进行过认真的梳理和扬弃，孔学汉服还在大行其道；执政党虽然本质上抛弃了意识形态追求，但“中国特色”的思维方式和“与人奋斗”的“其乐无穷”还是统治和愚弄的利器。甚至，鲁迅先生的“横眉冷对”和“横站”，竟被人们效仿于对待一切不同意见。“狼奶”开始用以比喻文革中红卫兵的残暴，但我觉得用以比喻从传统“升华”到党文化的精华更为恰当。我们生于斯，长于斯，受教于斯，你不喝狼奶，你喝什么奶？从五千年到六十年，除了狼奶还有其他奶吗？现在需要的不是否认我们喝过狼奶，而是如何消解狼奶在我们头脑中的遗毒。没有经历过文革的年轻朋友说到红卫兵，只有一种“妖魔化”的抽象；而其实，催生出文革中红卫兵行为的因子，存在于生于斯、长于斯、受教于斯的每个人身上。大家都还是“红卫兵”，只不过表现形式随环境而异而已。当今互联网上的一些帖子和文革大字报相比，差别只是：大字报多些口号和毛语录，现在的帖子多些西方哲人的论述；而其中那种“与人奋斗”的攻奸和置对方于死地的残暴，乃一脉相承。<br />
<br />
　　我们无法预测中国的未来走向，也不知道在环境、资源、经济等全方位压迫，和狼烟四起的群体事件之后，下一步会面临什么。但当局在今年已经明确声明：“绝不搞多党轮流执政、‘三权分立’、两院制。”（吴邦国全国人民代表大会常务委员会工作报告）同时，从去年12月10日以来，相信每个人也都感受到“喝茶”离自己越来越近。<br />
<br />
　　所以，如果我们无法期待和体制进行“良性互动”，无法寄望于体制的“自我改善”，而现实中又没有任何力量可以直接改变现体制的话，我们就只有等待，等待这个不思变革、拒绝还政于民的体制产生某种形式的突发。<br />
<br />
　　在这“无为”的等待中，我们是否应该考虑：当某一天面临选择的时候，如何重建制度？废墟上的建设能不能在民主进程上有本质的进步？当专制的压迫消除以后，一盘散沙的中国人能不能有良好的心态和修养以面对突来的民主权利？同时，对中国民主进程中发生过的种种，我们是否应该毫不留情地审视其中的缺憾？不是为了谴责哪个人，而是为了：吸取教训，净化自身心灵，消解狼奶遗毒，培养民主社会所需要的素养。<br />
<br />
<br />
北国游子<br />
二零零九年十一月五日</div>

							
							
							
							
