<br />
&nbsp; &nbsp; 河南工商行政管理局发布的公告显示，河南航空有限公司原名为鲲鹏航空有限公司，于2007年5月16日在北京市工商行政管理局登记设立，由深圳航空公司控股51%，另两家境外公司控股49%。2009年7月28日，鲲鹏航空有限公司住所变更为河南省郑州市新郑国际航空港迎宾大道6号。2009年9月25日，鲲鹏航空有限公司将企业名称变更为河南航空有限公司，股东未发生任何变化，河南未持有该公司股份。<br />
<br />
