按卦；中国房地产的疯狂算是过去了，2010年房市会开始显现疲软态势。价格走势上半年还能坚持目前水平。有波澜不兴之象，开发商的美梦和自慰心态到了秋后会被敲碎，尽管有各种优惠条件，却再也不会出现往年热市。他们也许会埋怨国家在稍晚时候出台新的土地政策带来的影响，实际上更多的是房地产需求和消费在国内开始成熟。前几年混水摸鱼大捞一把的投机商和开发商会像年老色衰的婊子一样感慨青春不再回来。按理说市场疲软，房价应该下降，但在2010年却未见有多大动静，是通胀压力所致，还是其他深层原因现在还不能妄加推测，但可以大胆预言的是，股市上，房地产板块泡沫现象会凸显，在下半年大幅度下跌，溃退至崩盘边缘。<br />
