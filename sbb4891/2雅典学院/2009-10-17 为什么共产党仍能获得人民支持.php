													<h2>为什么共产党仍能获得人民支持</h2>
						
						
																											<div id="postmessage_286663" class="t_msgfont">尽管中国共产党在世界范围内遭到有关侵犯人权的指责，但中共至今仍然得到许许多多中国人坚定不渝的支持，虽然这些人不可能忘却了他们早年在中共治下的悲惨经历，这到底是为什么呢？<br />
<br />
也许，对许多人，包括西方社会的人士，甚至包括当今中国的不少年轻人，这都是一个难解的谜团。<br />
<br />
英国《每日电讯报》马尔科姆・摩尔近日从上海发回一篇报道，试图解开这个谜团。这篇报道说，今年十月，中国共产党隆重庆祝了建国六十周年。60年前，毛泽东主席在北京宣布了新中国的成立。共产党建立之初，曾承诺建立民主、出版自由、司法独立的制度。然而，六十年过去了，这三者均没有在中国实现。<br />
<br />
取而代之的，是2000到4000万中国人在50年代末的饥荒中挨饿至死，无数人在文化大革命中受到无法言说的迫害。但是，共产党仍然受到人民拥护，这一事实长久以来令西方观察家感到惊讶。<br />
<br />
在上海一家曾是法租界的公园里，93岁高龄的钱秀珍（音）谈到她对共产党的拥护时，止不住泪流满面。“毛泽东当主席以后，那段时间是我过得最好的日子，”她说，社会秩序在那时发生了根本的、令人振奋的变化。<br />
<br />
“人民可以表达他们的观点。而之前，人民没权利说话。以前生活也很艰难，几乎每户普通人家都面临著我家所经历的困难。那是一个富人的世界，普通人生活都很难。”她回忆道。<br />
<br />
中国共产党在她三十多岁时取得政权，钱女士说，她早年的生活曾因1937年日本入侵中国而被弄得凄惨不堪。“现在回想起过去，我依然非常难过，我家有两个人在日军入侵时被杀害了。我弟弟被日本人的狗咬死，我父亲是被日本兵开枪打死的。”她说。<br />
<br />
“那个时候，每个人每月只能买2两油。也没有地方可以买米，要排很长时间的队，为家里抢购大米。法租界的警察会打那些争抢的人。而如果你足够幸运买到了米，也可能会被那些饿极了的人抢走。”<br />
<br />
那时，在蒋介石领导的短暂的民族主义时期，食品紧缺，人们都挨饿。她回忆说，那时候大家什么吃的都买不到。而毛泽东上台以后，她甚至可以提著一篮子花在上海的中心马路上闲逛。<br />
<br />
这篇报道还说，《南华早报》的记者在报道中，捕捉到了深埋在上了年纪的中国人心中的这一激动人心的瞬间――毛泽东在天安门城楼上宣布新中国的成立。<br />
<br />
如今钱女士每月有1800元人民币的退休金，每天坐在这座枝繁叶茂的公园内休息。“过去，中国人是不能进入这个公园的。这里是外国人的租界区，只有法国小孩能在这里玩。租借结束、外国人都回去了以后，这公园才成为了一个公共场所。现在公园非常漂亮，对我们这些老人来说也很方便。”<br />
<br />
而对于84岁的周杏芬（音）来说，毛主席改善了教育体制，给予妇女工作、学校的机会。“在旧中国，妇女没有发言权；新中国给了我们这些权利。在以前，我们只能整天呆在家里，对外面的事情一点都不了解。”<br />
<br />
在上世纪六十年代，超过三分之一的中国人是文盲；而在世纪之交，这一数字骤减至7%。中国的GDP从1952年的679亿元人民币增加到2008年的300670亿。<br />
<br />
周女士不无自豪地说，现在她的孩子们都生长在优越的环境中。“我住在一栋大房子里。过去我们生活很艰苦，我刚刚做的眼科手术要4000元，这在过去根本无法想象――我们连医院都去不起。现在我的孩子都挣得很好，他们经常请我在外面吃饭，这在以前也是不可能的。”她说道。<br />
<br />
在上海一条安静的弄堂里，77岁的张为民（音）和他的妻子住在一间简陋的小屋里。张先生的父亲曾是是一名富有的医生，但由于财产在文革期间被查抄，迫使他现在过著穷困的生活。尽管如此，他仍然承认共产党使生活变得“更加稳定舒适，尤其对老人们来说”。<br />
<br />
父母去世后，张先生参军了。结婚后，他的问题和责任迅速增加。“生活对我来说再也不容易了，随著对家庭责任的增加，我的生活质量大大下降，”他难过地说。<br />
<br />
然而，和许多中国人一样，他已经习惯了吃苦，对中国共产党建立的新社会充满赞扬。“1949年以前，60岁的人很少，而现在60岁只是小弟弟，一般人都能活到八九十岁――社会体制和生活质量的改善延长了人的寿命。”<br />
<br />
虽然他们都承认过去60年间的艰难，大多数中国人宁愿选择向前看而不是停留在痛苦的过去。“我父亲46时趋势，我母亲在三年自然灾害时被饿死，”86岁的蒯国英（音）女士说，“我们那时候没有吃的，连粮食都没有。我们五个兄弟姐妹中只有两个活下来的。”<br />
<br />
结婚后，蒯女士搬到了上海，而她的丈夫却因政府支援边疆的政策被派往中国的另一端――云南省。于是，蒯女士只能独自照顾包括她丈夫亲戚在内的九人大家庭，每月仅靠丈夫寄回家的20元人民币生活。<br />
<br />
“我出去找了份工作，每月可以挣40元，支撑起这个家。我丈夫后来被调到浙江宁波，他让我也搬过去，但我拒绝了，因为我还要照顾家里的老人。我们孩子还小的时候，没有足够的吃的穿的。我们就买布料回家，借著挂在蚊帐上的一盏灯连夜缝制衣服。”<br />
<br />
这篇报道最后说，如今，即使是蒯女士也支持共产党。她说，她的孩子们都移民美国了，他们老人有足够的钱，想吃什么就吃什么，想怎么花就怎么花。“现在生活好了很多，好了很多，真的要感谢共产党。”<br />
<br />
“我经常对我丈夫说，我们孙辈的生活完全不同了。不仅仅和我们过去的生活不同，和他们父母那一辈的也不一样。他们什么都不用担心，没有必要担心吃穿。过去，人们要很辛苦地工作来养活四口人，现在真是完全不同了。”<br />
