现主旋律倡导科学发展观；科学是一个外来的近代词汇。对科学一词的解释也各有不同。那么科学究竟是什么也是致关重要的。知识是人类对外部现实世界认知。人类的知识可以分成两部份即“科学与神学”。<br />
科学是一种去伪存真的求知方法。科学也是一个去伪存真的知识体系；科学倡导动态求知，探索真理的精神。凡背离了“去伪存真”的求知方法都是属于神学。神学信奉“信则灵”。神学存在绝对真理。华国锋所供奉的两个凡是；实质就是反科学。用神学来治理国家。<br />
现主旋律倡导科学发展观，就是一个时代进步，倡导科学发展观就应以追求社会发展必然规律，站在推动社会发展的一方，以科学的态度寻找治国良方；以追求真理为已任；科学发展观的倡导者，必定懂得“去伪存真”的科学基本原理，必定懂得，言论自由是追求真理最基本的前提；禁止言论自由，必定害怕真理，肯定不是科学发展观。<br />
现中国特色论又供起了“中学为体，西学为用”神位，以此走出一条与世界潮流不同中国特色的路。是忽悠，还是真理，稍有点理性，即可心知肚知。<br />
科学是一门“去伪存真“的学问。科学知识体系中都有一套完整独立的概念，逻辑推理，求证构造起来的。以数学为例，数学由一些关于数的基本概念与数学公式等构成的一问学科。数学是一问工具学问，参与数学学习是一个求知过程。数学学者权威建立可实证的基础上。数学也是一问与社会同步发展的学问。<br />
而神学则不同，神学中则以宗教最为典型。每一个宗教都对有自已的教义，自已的权威。每一个宗教教义与权威是不容质疑的，要求信徒无条件的信仰与服从。以佛教为例，教主释迦吗呢，人生因果论，信佛教的对些都要求是深信不疑。佛教徒对教义理解，修心均以佛教基本教义为准绳。<br />
<br />
