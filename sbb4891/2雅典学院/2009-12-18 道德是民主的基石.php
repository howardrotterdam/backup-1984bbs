那么道德来源于哪里？道德的基石是什么？是起码部分来源于基督教系统吗？记得一个美国大法官是这样说的。 </blockquote></div>就道德的起源，就是一个典型的伦理学说面临的话题了。但道德之所以形成，总体是为了协调人与人的关系，即保持社会性的，或使社会群体安然无恙的一种行为规范。<br />
虎毒不食子，这是老虎的道德，这种道德是非意识的，相当于人不吃人肉一样，母亲保护孩子一样，还包括近亲不结婚（譬如兄妹结婚系乱伦，同胞胎的狮子交配也是不允许的），这属于“低级道德”即物种得以生存的伦常，还有蜜蜂、蚂蚁的社会性群居都需要道德的维系。这种伦常的形成系达尔文的自然选择的结果，或者按照优胜劣汰的方案，没有形成有利于物种生存的“道德”的物种就会被淘汰。<br />
人类进入文明时代后，人与人的关系逐渐复杂，人类要生存，就需要很复杂的道德体系，这种体系的延伸会发展到强制性，即法律，我们之所以说道德是法律和民主的基石，意思就是在这里（当然，这种法律体系了哪些人的道德价值观或赤裸裸的利益，又另当别论了）。<br />
道德只是一种行为规范，只建立在价值观之上的一种行为规范，没有好的道德和坏的道德的区分，只有适合不适合社会发展的道德的区分。<br />
不同的社会，或者社会的发展，需要道德的相应发展，如果社会发展了，道德没有发展（就是物质文明发展了，精神文明落后了，呵呵，这是TG的说法）就会产生冲突，甚至有先知者大呼要打破旧道德，建立新道德，即所谓的思想革命。<br />
为什么会出现这种矛盾，那就是道德的惯性作用，即鲁迅先生说的，即使搬动一张椅子也很困难。但我们有时不得不搬动。不是说搬动后就“道德了”，是搬动后更适应现代社会。<br />
之所以搬不动，还有有人故意不让搬，因为这符合少数人利益，而这少数人手里又有枪，虽然不搬动对整个社会的发展有阻碍作用。这个有枪的人还雇人说不搬动更好.......<br />
我们之所以痛恨中国文化，痛恨中医，痛恨中华大统，其实都源于这些宣传来源于当今TG的政治宣传，而这种宣传有往往是转化矛盾、维护统治，遮蔽事实（甚至是歪曲历史的）宣传。但如果撇开这一层，中国文化、中医、民族统一（当然不是武力统一）并非坏事。<br />
很多学者在讨论中国的国民性，人性、文化感叹中国这些文化特质不太利于现代民主思想的诞生，说到底就是搬动椅子的阻力很大，而现代经济又要求这种民主的产生，这就是中国当今的最大矛盾与冲突。<br />
社会制度的发展，为什么要强调道德，很多人以为以道德的形式约束官员是很幼稚的想法，的确是的，但道德不仅仅是两袖清风心怀天下，还要建立完善的监督与制衡机制，这本身也有道德的成分，既然道德是建立在价值观的基础之上的行为规范，那么在这个基础上建立的法律自然也是符合价值观的。<br />
没有这个基石，一切都是扯淡。<br />
