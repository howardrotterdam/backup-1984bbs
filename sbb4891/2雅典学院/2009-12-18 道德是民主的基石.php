													<h2>道德是民主的基石</h2>
						
						
																											<div id="postmessage_381021" class="t_msgfont">写下这个标题，是源于中国国内的一篇报道，报道的原文是：<br />
 <br />
&nbsp; &nbsp; 奥巴马17日在亚利桑那州菲尼克斯演讲时，一群人在演讲会场外示威，其中有十几个示威者公然携带枪支。<br />
<br />
&nbsp; &nbsp; 一名示威者甚至肩挎“AR－15”半自动步枪。<br />
　&nbsp; &nbsp; 警方说，17日示威活动中没有出现违法行为，没有人遭到逮捕。不过，警方对那名“AR－15”半自动步枪携带者“格外关照”。 <br />
 <br />
<br />
&nbsp; &nbsp; 看见这个报道，我觉得感到万分激动，激动的不是有示威者可能向总统开枪，而是在那样的一个国度，人们是如此的恪守社会契约，即使是总统，也没有丝毫超越的权力。而关于这种情形，连同美国的校园枪击案，在美国本土也引起过争论，关于美国的枪支管理，实际上在美国一种都在争论不休。<br />
<br />
&nbsp; &nbsp; 但我关注的不是这个，我甚至在想，倘若在中国，也有类似的情形，比如温宝宝访问某个贫民窟，有反对者拿着西瓜刀站在路边，也并不违法，是不是警察会以国家领导人的绝对安全为由把那个家伙逮捕了呢？更要紧的是，即使是非法逮捕，可能全国人民也会谅解警察的行为。<br />
<br />
&nbsp; &nbsp; 在西方就截然两样了，总统仍然享有特权，但都是通过议会立法来赋予的，比如总统的坐骑，是防弹的，防弹玻璃的钱是国库出的，这都是有法可依，无法可依的，即使有人拿枪站在那里，你也没有办法。<br />
<br />
&nbsp; &nbsp; 我们又反过来想，倘若总统的保镖为了总统的安全，违法一把，先把那个示威者按在地下暴打一顿，然后带走，恐怕这个保镖就要给总统惹下很大的麻烦，因为总统的违法（这里的“总统”应该是机构，包括了他的帮手之类的）的严重性不是行为的直接后果，而是对法律尊严的蔑视与践踏，这样，全美国人民都不会答应。<br />
<br />
&nbsp; &nbsp; 为什么美国人民非常在乎国家机关、公职人员对普通老百姓的侵权呢？其实道理很简单，国家机关是有组织的，强大的，不时刻监督，他们可能就会学坏，防范国家机关、公职人员违法，其实就是在维护他们自身的利益，或者在维护长期以来人民奋斗而来的民主制度与法治理念。<br />
<br />
&nbsp; &nbsp; 在民主思想成熟的国家，如果哪个家伙要破坏规则，比如国防部发起兵变出来统治这个国家，基本上会遭到全国人民的反抗的，他们自己也知道那样干是自找死路，谁还兵变呢？当然，军队国家化还有很多制度在规范制约，但成熟的民主思想及一致核心价值观却是最根本的保障。<br />
<br />
&nbsp; &nbsp; 说了半天，还没有与道德沾上边边，不急，下面马上就绕到了道德上面去了。<br />
<br />
&nbsp; &nbsp; 我们说的核心价值观或民主思想，其实就是一种社会道德，查查维基百科，道德是这样定义的：<br />
<br />
&nbsp; &nbsp; 指衡量行为正当与否的观念标准，一个社会一般会有社会公认的道德规范，涉及个人、个人之间、家庭等私人关系的道德，称私德；涉及社会公共部分的道德，成为谁会公德。<br />
<br />
&nbsp; &nbsp; 从这个解释来看，我们通常把道德理解为一个人的行为规范，其实，我以为，道德的内涵远不止这么狭窄，他其实是人类社会行为的总和的一种观念标准，譬如我们可以说，一个专制国家对老百姓的人权侵害是不道德的，比如在封建社会发展地方武装反对皇权致使国内战争生灵涂炭是不道德的。这里的道德，其实就是建立在人类普遍适用的，或社会公认的价值观之上，而道德的行为规范即是附和这种价值观一种行为规范。<br />
<br />
从这个角度来分析问题，我们很容易看出东西方关于“国家领导人安全”的不同态度，在西方，人民知道了民主的好处，知道遵守法律的好处，并把肆意破坏，特别是国家机关破坏法律的行为定义为最不道德，在他们看来，总统的安全远比总统公然挑衅法律的尊严要小得多，因为他们知道，人们一旦失去了依法治国的这个原则，将会给社会带来极大的危害，这才不顾一切的为之奋斗，甚至献出自己的生命。这在在美国社会进步的历史上是不鲜见的。<br />
<br />
在中国，由于文化与历史的差异，几千年的皇权思想及人治理念往往使我们想到皇权是大于法律的，虽然也提出过王子犯法与庶民同罪的相对进步的思想，但这往往也是在整个皇权社会下的佐料或权衡贵族间的矛盾和愚民的口号而已。<br />
<br />
然而到了现代国家，人际关系由简单的自然关系逐渐转变为复杂的经济关系，利益冲突日益尖锐的背景下，旧中国的皇权思想将不再适应社会的发展，关于这个，人们已经普遍认识到这一点。但如何一跃成为真正的现代民主国家，在中国却是仁者见仁智者见智的。<br />
<br />
而我却大胆的认为，道德，依靠道德。西方的普世价值观告诉我们，一个国家，要跳出暴力革命的循环，只能通过民主、选举、宽容的方式，只有这种道德价值观的建立，才能在此基础上建立适用的法律，即宪政的实现。换句话说，只有使用道德力量摧毁一切旧的制度，才能在此之上建立新的法律体系。没有这个基础，法律只是一纸空文，只是反复被一个国家的强势群体利用。这就是为什么中国宪法明明规定的许多权利我们无法实现，国家公职人员的种种特权我们看在眼里骂在心里却无法改变。因为，我们现在缺失这种价值观，我们的行为仅仅是利益的权衡，觉得自己的反抗不仅毫无用处还可能找来麻烦。没有为自己的尊严与价值观而奋斗的原始冲动。<br />
<br />
对民主的信仰，只有上升到道德层面，而不仅仅是就事论事的维权，才可能超越。因为信仰是属于人类的，而利益权衡只是个人的，短暂的。这里我举一个简单的例子，假如我们都不相信来世，那么个体生命的结束将意味着自我世界的结束，在这个前提下，有人告诉你，100年后你将死亡，这并不会给你带来任何恐惧，因为这是事实，但有人告诉你，100年以后地球将大爆炸，你可能会感到绝望与恐惧，或者，整个人类会立即进入一种恐慌与悲观的情绪中去。这是为什么呢？这里其实就有一个人类的整体信仰的问题。或者，一个革命者为了人类的未来，为了国家的未来，为了民主的信仰，可以抛弃自己的生命，这也彰显了信仰其实已经不属于个人，它属于整个人类。<br />
<br />
一个民主国家，通过全体人民的努力建立了一整套民主制度，对这种制度的维护与渴求维护，其实就是道德养成。但一种价值观，或“观念标准”得到社会的认可，就形成了新的道德体系。人们就会用心维护，即使有时看起来似乎“事不关己”，维护民主制度第一实施与保持，以确保将来不再发生没有民主的悲剧。<br />
<br />
道德是软约束，是自省与舆论约束，但同时具有强大的力量，这种力量来自于自觉与发自内心的呼喊，法律的建立必须基于这种价值观的实现，即道德的基石。没有这个基石，专政与皇权思想就会肆意蔓延而最终吞噬这个社会，吞噬所有的良知与公平。<br />
<br />
而我们要做的，就是，告诉大家怎样的制度是最道德的，现在的制度其实是不道德的。这是另外的话题了。<br />
