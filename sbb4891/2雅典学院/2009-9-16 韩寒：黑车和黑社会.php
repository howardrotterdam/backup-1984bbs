													<h2>韩寒：黑车和黑社会</h2>
						
						
																											<div id="postmessage_242951" class="t_msgfont">前天我看见一个新发的帖子，觉得很正点，转在了博客上。早上南方一家媒体给我电话，说当事人是你么？我说不是，记者感叹道，哎呀可惜了，如果是你就是好新闻了。好在很快她想明白了，不是我是更好的新闻。上海的媒体问明了出处后问我，你相信么？我说我没有时间去查证，但是我相信的，因为这事太黑了，黑到连黑社会都会被雷住，所以一定是我执法机构办的事。<br />
<br />
这让我想起五年多前在松江的一件事情，正当我停车在路边时，突然有人敲窗，拿着手电一阵晃。我把窗摇下一个小缝，我和窗外同时问道，干什么的。我定睛一看，原来是疑似联防队，我的汽车的前后左右都已经停满了他们的摩托车。他说，下车把证件拿出来。我说，你先把证件拿出来。<br />
<br />
当时那人就把手从窗缝里伸了进来，我第一反应就是关窗。这是我第一次痛恨汽车的人性化设计，这时候汽车的车窗关闭防夹功能启动了，窗不但没关上，还往下降了不少。他当时揪住我的头发就往外拉，我只能开车连人带摩托车撞了就走。后来还有几个人开着摩托车在后面追，我当时心软了，没有再撞他们，因为已经有了一定的速度。<br />
<br />
我只想告诉大家，以后遇上执法人员的非法执法，在保证自身安全的前提下以暴制暴是唯一的方法，比如说，查车不出示证件就往车里伸手的，可以考虑用窗夹住手以后割下来，当然，割下来以后还是要还给人家的，否则你就是偷窃了，如果有不愿意表明身份的人对你的车进行了堵截和对车主企图进行伤害的，则应撞死一个算一个，坚决不下车。如果遇到有人倒钩诬陷你开黑车的，当人扑上来抢钥匙，有刀的则应该一刀捅向对方，天知道你要干嘛，如果有人闯进你的家里掏不出证件的，则应当做抢劫处理，去厨房提菜刀，如果在街上有人突然把你小摊或者摩托车给抄了，也应该以抢劫论处，当施以围殴和反抢以自卫，如果在围殴过程中对方掏出证件，则应停止殴打，但是对方的医药费要自理。只有以暴制暴，对非法执法的人员动用一切法律允许的工具进行自卫和反抗，这个国家才有文明执法的希望。<br />
<br />
闵行黑车事件最恶劣的地方还不在于非法执法，而是利用私车主的社会公德心进行欺诈。逼良为娼已经不算什么了，因为你一逼，人家好歹也为娼了，但是诬良为娼真的很少见。而且是处心积虑的对善良的私家车车主进行迫害，闵行交管部门所为的危害已经超过了野蛮执法和违规执法的范畴了，因为对于这两者，我们都已经很习惯了，你哪天温柔执法或者合格执法了，我们这把贱骨头还会觉得不习惯，总会觉得难道我今天穿的像个外国人？所以说，其实国人是允许在执法过程中，为了突出“威严”，表现的比较强盗一些。但是，闵行区交管部门的行为就是道德灭绝。说实话，在这个社会上，如果你生病了或者家里有急事需要搭车，有人愿意让你上车是很罕见的，这样的人是珍稀的物种，是单纯的好人。闵行区交管部门做的事情说简单点，就是将这些单纯的好人从茫茫车海中分辨出来，拘押下车然后罚款一万。<br />
<br />
 <br />
<br />
至于黑车，其实我和大家的观点不一样，我并不认为黑车是一个必须扑灭的对社会有着巨大危害的事物。但是为什么要扑灭黑车，为什么又扑不灭呢，我来打个比方。<br />
<br />
比如说，某个外国有个黑社会（这个比方当然要打到外国去，因为只有外国才有黑社会），在它管辖的地方，有人成立了一个公司，每年都向这个黑社会交保护费，但是这个公司对员工的剥削非常的严重，无奈因为法律规定，员工要干这个活只能加入你的公司，所以员工们也没有办法。但是突然有一些人，做的是和这些员工一样的事情，却不用向这样一个剥削性质的公司缴纳任何费用，缴纳过费用的员工当然就不服了，凭什么我们交那么多钱，还有人和我们抢生意呢。这个公司当然要找黑社会反映问题，为什么我们交了这么多保护费，还有人和我们抢饭碗。黑社会老大一听，很火大，派出手下处理这个事情，结果手下一打听，这事情还不好处理，首先是这个公司的确太黑了，其次是那些抢饭碗的势力也慢慢壮大，不光形成了小规模的组织，甚至还渗透进了这个黑社会。但是毕竟收了保护费要有个交待的，而且老大给了一个额度，所以只能抓一些过路人装装样子。<br />
<br />
那么，如果解决这个外国黑社会面临的棘手问题呢？其实很简单，收保护费的地方有很多，你也不差这点保护费，不要因小失大，索性告诉这家公司，你丫反正也赚够了，就散了吧，开放这个工种的限制，然后向每个工人收取合理的保护费。<br />
<br />
 <br />
<br />
同上，如何解决黑车的问题呢？就是消灭剥削性质严重的出租车公司，取消配额，让所有的黑车都变成合法的出租车，这样就大大提高了每一个出租车司机的收益，可以让他们使用更安全更好的车型，而不是车子很破，一张可以运营的纸可却是车价的十几倍。成立一个非盈利机构，只对出租车进行安全管理和登记，国家直接向每台出租车收取合理的所得税，事情就解决了。出租车引发了多少群体事件，国家又不差那点钱，何必呢。 <br />
