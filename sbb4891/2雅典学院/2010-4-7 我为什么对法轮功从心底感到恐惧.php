													<h2>我为什么对法轮功从心底感到恐惧</h2>
						
						
																											<div id="postmessage_552539" class="t_msgfont">就“法轮功”这个概念代表的含义，我认为有下面3层： <br />
<br />
它表面是一种气功心法<br />
它实质是一种邪说<br />
它的组织具有煽动性，它反共<br />
<br />
如果仅仅是作为一种气功心法，法轮功肯定不会有这么大的影响力，也不会令人恐惧。它可能只是国内众多气功中的一种，所有气功都会宣称能治病，比如我很久之前就知道的一种“香功”的气功，现在依然存在。如果法轮功只是宣称治病，那么可以想见，现在会和各种气功一样共存于中国。<br />
<br />
但它不仅仅只是宣传治病，还杂糅了佛道等文化，提出“修炼”的概念，提出对教义和教主的“绝对服从”，这就开始邪了。香功之类气功，也宣称能把肿瘤从人身上转移到树上这种匪夷所思的奇迹，但没有对人进行思想控制。还是要重复上面的说法，如果法轮功仅仅是一种气功心法，那么它没有任何危害，可惜他不是。它对宇宙有自己的一套荒谬的解释（且不说有多荒谬），它对教民进行思想统治，这也是它被成为邪教的原因。<br />
<br />
法轮功组织具有煽动性，但煽动手段及其低下，甚至不惜以谎言对抗谎言。从刻字石碑到退党人数，从QQ、电话上的“朋友，我给你讲个故事”到各种八卦，没有任何新意。我猜这跟李教主的文化程度不高有关。<br />
<br />
最后也是最重要的一点，也是法轮功得到某某再世之流支持的最主要原因，就是它激烈反共。然而敌人的敌人不一定是朋友，尤其这个人满嘴的谎言，“以谎言对抗谎言，你得到的一定是谎言”，谎言，让法轮功的反共变得毫无意义。如果，假设，某一天法轮功反共成功，最后执政，它会变得更加残暴和独裁，因为它是用更残暴更独裁更胡言乱语战胜了残暴独裁胡言乱语。一想到这，就让人不寒而栗。<br />
<br />
我实在无法理解为什么会有人支持法轮功。<br />
<br />
（本人已经看过诸位对法轮功的看法，讨论可以，请勿骂人，也不要逼我骂人。）</div>

							
							
							
							
