丙午丁未之灾&nbsp; &nbsp;<br />
2026，中国不可避免出现大灾难？ <br />
杨绛女士曾经写过一篇回忆文革的文章，叫《丙午丁未纪事》。写的是1966、1967年的事，却冠以丙午丁未之名，的确让人费解，其后看到洪迈《容斋随笔》中的《丙午丁未》一文，矍然醒悟，却也不由心惊。<br />
洪迈说，中国每逢丙午丁未之年便会出现大的灾难，不是内忧，便是外患。洪迈当然不是空穴来风，他举出了三代以下至他所处年代的所有丙午丁未之年所发生的大灾难：<br />
======<br />
公元前195年（丙午）：汉高祖刘邦驾崩，吕后开始专权，汉家基业几乎倾覆；<br />
公元前134年（丁未）：戾太子出生（巫蛊之惑孕育），汉朝开始征伐匈奴，由此征战三十年，死伤无数；<br />
公元前74年（丁未）：汉昭帝驾崩，刘贺登基，旋因淫乱被废，一岁再易主；<br />
公元前15年（丙午）：王莽被封为新都侯，赵飞燕被封为皇后；王莽篡权祸根种下，西汉绝亡；<br />
公元46年（丙午）：海内无事，但勾引南匈奴，导致后来刘渊乱华之祸；<br />
公元106年（丙午）：汉觞帝即位，第二年汉安帝即位，东汉政乱始自此二年；<br />
公元167年（丁未）：桓帝驾崩，灵帝即位，汉朝从此衰亡；<br />
公元226年（丙午）：魏文帝驾崩，魏明帝即位，司马懿受命托孤辅政，其后司马氏灭掉曹魏，源自此年；<br />
公元286年（丙午）：慧帝尚处东宫，五胡乱华，源自于此；<br />
