													<h2>才，胆，识，力，韩寒都具备，百年难得的明白人。</h2>
						
						
																											<div id="postmessage_385377" class="t_msgfont">我80的，刚开始比较喜欢韩寒，长大了，总是看不惯韩寒的拽，鄙视他，直到有一天我看了他的博客，佩服之情油然而生（靠，这个词好像自小学以来第一次注入感情用啊，哈哈。）。不由得感叹啊，就像有网友说的：喜欢韩寒其实是很无奈的事。谁叫中国只剩这么一个敢将良心和才华结合得恰到好处的作者？――能做到的人不敢这样做，敢这样做的人又没那能力。我喜欢张老师，也喜欢韩寒。文坛和学坛的硬骨头，民族的良心啊。<br />
 <br />
正直的人喜欢韩寒，龌龊邪佞之徒害怕韩寒，韩寒是中国年轻人的骄傲，愤青的克星。<br />
 <br />
韩寒只是一个正常人，荒谬的是这片土地！正直的，只要非偷鸡摸狗者都喜欢韩寒。<br />
 <br />
看韩寒博客里的东西有种畅快，我在大学里面每天都是一些政治思想的，专业课都还没有开，觉得特压抑，韩寒就是最好的心灵慰藉，觉得果然写的东西有条理又深刻，不象一些砖家，道貌岸然，阻止新生力量发展，<br />
 <br />
看韩寒的小说吧,写得很好.十六岁时的文字让我惊呀,与古今大家有一拼.如诗:&lt;&lt;苏幕遮.绝情&gt;&gt;:断愁绪,空山居,天涯旧痛尽染入秋意.缘尽分飞誓不续,时尽寒冬,问他可寻觅?缈苍穹,淡离别,此情已去,愿君多回忆.我欲孤身走四季,悲恨相续,漠然无耳语.&lt;&lt;少年游.忘情&gt;&gt;:待到缠绵尽后,原重头.烟雨迷搂,不问此景何处有,除却巫山云.两心沧桑曾用情,天凉秋更愁.容颜如冰,春光难守,退思忘红豆. ......很多,现代诗也写得很好,如:悠悠爱恨之间,我心永远不变,纵使沧海桑田,追逐你到天边,我不在乎昨天,我无所谓明天,抛开世间一切,唯独对你思念. ......我想如果他不赛车把精力都用在写作上也许更好.<br />
 <br />
韩寒对庞然大物的那份轻蔑真好。<br />
<br />
韩寒，张鸣，属于拒绝当局思想奴化的个性独立的学者，寒韩对令人恶心的现状的批判角度新颖，因为没有被当前教育体制所奴化，也就没有被当局奴化意识的烙印，属于一树奇葩，绽放于体制之外，揭露很多当局表演出来的秀，但当局也要顾及脸面的，不能公然下手拔了这眼中钉，既然善于表演，会一直表演下去，以后会更小心，不然表演出丑，辛苦白费是小事，丢人现眼是大事。<br />
互联网的普及，让平民百姓比较容易快速了解即时事件，而且如果农村发展起来后，网络会迅速启发国民的思想文化。最近看到网络主流网站对腐败现象及社会丑恶现象报道的跟帖，感觉到现实中，一大群有理性的网民正在形成，一个公共责任心健康的中国人群正在形成，网络民意“离经叛道”且“返璞归真”，我对这代年轻人，欢欣鼓舞的，。。。。。不过这应该是当局不好受的，哈<br />
<br />
前一段时间看《鲁豫有约》采访韩寒的那一期，让我感到表面争议不断但是绝不妥协的韩寒，其实内心满是善良和责任，甚至在镜头前举手投足间还有羞涩和不自然。这与看他的文字给我的感觉判若两人，我一直以为韩寒是李敖似的，金刚怒目，嬉笑怒骂，而现实中的他，至少在演播厅里的他，却是频频往沙发角落里坐的人。没有名牌，没有化妆，没有丝毫的掩饰；没有空话，没有假话，没有虚伪的做作。<br />
<br />
有人说他是幸运的，我们往往把一个人的成功单纯的看做是幸运的结果，却没有看到成功人士一路走来的筚路蓝缕，荆棘满地。我承认他的幸运，他的成功的不可复制，上帝最终眷顾了他。其实我们每个人心中都有一个幸运之神，问题是你是否由努力和真诚把她唤醒。<br />
<br />
韩寒是幸运的，他的幸运和他的选择密不可分，在地震灾难面前，在社会不公面前，在假大空满天飞，伟光正横行的面前，作为一名公民，他尽到了自己的责任和义务，并用自己的影响来把这种声音传的更远。<br />
<br />
直感叹这样的有骨头的人太少了，很难推动“风车”的车轮，多的是自伤。<br />
 <br />
许多人看了韩寒的文章,改变了对80后的印象,他们不只利益,有的人也有原则,有不敢触碰的道德底线.韩寒的文章看的很少,感觉是他很独立.<br />
 <br />
韩寒之所以是韩寒，与他远离中国式正统教育和狗屁体制，有很大的关系，特别是前者，他因此没沾上很少国人能够幸免的奴性或主性。看过何东对他的访谈录影，想不到如许斯文，很不错。<br />
 <br />
韩寒之类的人是中国转型成为公民社会的希望,非常支持韩寒!<br />
 <br />
在美国的孩子中，能见到很多韩寒，中国只有这么一个，还被当成叛逆。<br />
 <br />
我喜欢韩寒,不是现在喜欢,而是在他还是上海的一个高中生写新概念作文时就看出这个孩子不简单.他的三重门我也看过.现在更喜欢他了.他是一个具有天然自由思想和理性精神的人,是非分明,智慧超人.<br />
 <br />
鲁迅如果活到现在，我敢断言，即使没有打成右派，文革中也得被整死《或者投湖自杀》。韩寒，你别臭美，你将来的下场不会比我为鲁迅所设计的归宿好到哪去：你们是一丘之貉。<br />
<br />
韩寒是敢于说真话的人，而且韩寒想说就说，无所顾忌，这在大陆相当不易。<br />
