																											<div id="postmessage_384513" class="t_msgfont">来源: 新世纪周刊(北京)<img src="http://i45.tinypic.com/5umxr5.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
选韩寒当市长<br />
这一点也不耸动，在国外，市长可以是一个高中生、嘴上没毛的小伙子，而韩寒，10年前的那个退学高中生已经刮着胡子出现在时尚类杂志上，冷峻而调皮地看着杂志之外。<br />
<br />
10年了，这个少年作家退学10年了，然后变成了一个青年车手，变成了全中国最有文化的司机。不喜欢的人还是觉得他嘻嘻哈哈，但是喜欢他的人越来越多――他们喜欢他对公共事件锐利的、一针见血的评论，喜欢他一个比喻就把装正经的人推倒在地的那种智慧。<br />
<br />
据说长于比喻乃是哲学家的大智慧，孔子、苏格拉底、佛陀和耶稣（以上排名不分先后）都是如此。<br />
<br />
在他退学的10年里，中国主要城市的房价是10年前的8倍乃至于10倍，中国的税收多数年份都以40%或者更高的比率增长着。<br />
<br />
自古人们相信有智慧有操守的人，但近几十年，人作为个体被认为是不可信的，只有单位才被社会认为是可靠的，正如同一个人无法自己宣誓自己未婚或者无子女，一定要工厂街道扣一个红印，才算有效。<br />
<br />
这也是为什么韩寒受到人们的欢呼，一个真实的、活生生的个人在我们面前，他聪明、有勇气说真话，用赛车和写书养活自己，不寄生于某些协会组织，“我们”不用担心他和“他们”同流合污。<br />
<br />
我们需要韩寒这样的市长。<br />
<br />
韩寒的体制外十年<br />
<br />
10年前他退学了，成千上万当时的成年人等着看他天才泯然众人的笑话。10年后，成千上万的成年人说他是公共知识分子、社会良心，还有人说要选他当市长<br />
<br />
少年和父亲一旁侍立，手里握一份休学申请。校长正在和一个即将派去香港的优等生谈话：“香港天气很热的，衣服可以少穿一点。去了那里，千万要给学校争光啊??”温情流露似无止境。<br />
<br />
盖完章出来，父亲说：“以后不要再让别人这样看不起。”<br />
<br />
时光定格在少年表情模糊的脸上，然后，呼啦啦拨过10年。少年的头发飞快地变化长短，肤色渐深，胡渣时现，笑和不笑的样子却依稀。因为瘦，脸型越发紧窄。他从17岁长成27岁。<br />
<br />
他用了10年为证。<br />
<br />
“我只是告诉你我不认同你”<br />
<br />
18岁的韩寒坐在两位教授旁边，一头江口洋介式长发，表情无聊。一名专家以现代性理论分析社会转型与韩寒现象之间的关系，与会观众纷纷以自身经验消解韩寒经历的独特性，如果他本人开口要说些什么，马上就有一波更高的声浪盖过来。<br />
<br />
他于是沉默，面上笑意微谑。 <br />
<br />
一名留着一双长辫的女士，激情告白了自己的教育背景和人生经验后，大声质疑：“你平时上网聊天是用ICQ还是OICQ还是聊天室？”<br />
<br />
而后她宣判这个少年不成熟：“成熟的人都用ICQ和OICQ。”<br />
<br />
这是2000年的《对话》节目。<br />
<br />
一个偏科、留级并退学的学生得了几次作文奖，写了两本畅销书，就自以为是滥发议论，这是当时的韩寒给大多数人的印象。多数人轻易把韩寒理解成青春期的叛逆躁动，所以每个人都觉得自己有经验有资格教育后进，急于向这个不知天高地厚的毛头小子展示自己的人生感悟，担心他终将败于夜郎自大或者江郎才尽。为了证伪他所谓的“全面发展全面平庸”，节目组还特地找来一名同龄的“全面发展全面优秀”的女生，主持人当场问韩寒，这女孩是否像他小说中的女主人公。<br />
<br />
没人拿这孩子当一回事。节目中专家预测韩寒可能只能走三五年，就不能保持他的“纯粹性”：如果要走五十年，还得进大学。<br />
<br />
可惜这孩子一直是我自己玩不带你们的姿态。三年后，他已经是一名车手；五年后，他已经是一名冠军车手。他可以通过赛车和写作两种方式养活自己。<br />
<br />
所以他不希罕文坛对他的待见或偏见。这名中国最有文化的高中没毕业的司机，2006年3月，掷地有声地抛出博客《文坛是个屁，谁都别装逼》，很黄很暴力地回应了文学批评家白烨对他及80后作家的批评。<br />
<br />
言辞的轻佻可能被看成后生的诞妄，但针砭文学界的风气却很到位。末了他一摆手：“我发表完观点了，不参与任何愚蠢的笔战论战之类。我很忙，我要进入车坛。”<br />
<br />
事件当然不因为他这一句轻描淡写而收尾：亲友团，父子兵??几乎整个三月，伴随着不断更新的笔战，以及粉丝团的搀和，韩寒“狂妄自大目无尊长的小青年”形象深入人心，即便他说的东西，仔细看来其实话糙理不糙。<br />
<br />
回顾三年前，韩寒认为自己不是笔战：“我只是告诉你我不认同你，然后我的观点是什么。那些看起来是写给那些人的文章，写给争辩时的文章，事实上我并不是写给他们看的，我是写给读者看的。”<br />
<br />
那一年也是他开始写博客的时间。因为韩白论战，很多人对他的印象被加固：一个不依不饶、好胜自负的年轻人，他们看他的博客是为了看热闹。但渐渐地，人们发现，他的博客内容，不只是他身上发生的事情，不只是他所在圈子内的事情，还涉及到很多与他看起来没有直接利害关系的公共事件，他热衷于管闲事，亮明自己的观点。某件事发生后，上韩寒博客看看他怎么说，成了越来越多的人的习惯。韩寒的犀利、才气与幽默感，使他容易直抵事件核心，以明白巧妙的比喻，揭露被粉饰的利益关系。这类文章让多数人觉得快意，包括他自己，因为他想对一件事发言的时候，那就是，马上，为此可以不赚稿费。<br />
<br />
当他的不依不饶与自身无涉，而是为了展示某桩公共事件的真相的时候，他身上那些原本被标签为80后通病的品质，突然让人感到熠熠生辉。<br />
<br />
人们开始称道问题少年的成长与成熟，也不再紧张80后一如洪水猛兽――10年里，大家都各自老了一点，宽容一点。<br />
<br />
好在韩寒不会受宠若惊。瘦削灵巧的他像《十日谈》里的诗人吉多・卡瓦尔康蒂，被人包围的时候，能轻灵一跃，跳脱出去，无论那些人是来说他坏，还是说他好。别人总结他的成长轨道多么磕磕碰碰，对他自己，却都只是最简单最自然的选择。<br />
<br />
只做自己想做的<br />
<br />
韩寒一直不算有远大理想崇高目标。他当初之所以想要进重点中学松江二中，因为想和当时的女朋友在一所学校。而他自知成绩不如，靠长跑加了8分，算是体育特长生。<br />
<br />
不过他不喜欢长跑这样呆板的运动。老师如要求他平时参加训练，他必在下次比赛中跑个第二，以证明自己不宜和其他人一样训练。<br />
<br />
体育老师很惋惜，认为他在浪费天赋，不勤加训练是无法当上国家一级运动员的。<br />
<br />
不过他证明了不勤加训练长跑，也能投身体育事业的，因为他找到了更醉心去付出的事业：赛车。<br />
<br />
当初之所以提出休学，因为学校当时已有让这个成绩拖后腿的学生退学或处分的意向，两害相权取其轻，自己休学，毕竟面子上好看一点。<br />
<br />
那时候他已经留级一年，因为七门功课不及格。同时，也已经是新概念作文大赛一等奖获得者。他上语文课时不作笔记不听讲，老师批评的时候，他说：“我是班里语文最好的。”<br />
<br />
这个学号垫底、上学期语文考试60分的学生的大言不惭，激怒了老师。那段时间里，韩寒的父亲被叫到学校听训，韩寒当着老师和面挨父亲的打是家常便饭。韩寒非常冷静地回忆：“其实我非常理解这个事，我爸爸当时气得就是想打人，他又不能打老师，只能打我；其实老师也很生气也很想打我，但是他自己不能打，所以只能把我爸爸叫来。”<br />
<br />
他觉得自己没错，语文能力跟语文考试是两回事，班里最好，在他还是谦虚的说法。为了证明自己的水平，韩寒一度投身创作，给《少年文艺》、《萌芽》、《少男少女》等杂志投了不少稿件，到发表后，他就写得少了――他证明了。<br />
<br />
退学之后，年轻人自然有一些迷茫，但晃晃悠悠也不失是一种过法。他的驾照，就是在这段时间考出的。同是新概念作家，与韩寒颇有私交的刘嘉俊回忆，车手韩寒当年要考一个驾照也是考了N次，反复忍受了“上课，课程的教条，考试和不及格等等他一直不喜欢的感觉的折磨”，“好几次，他跟我说明天驾照考试。然后过几天跟我说，没过。然后回去鼓捣他的摩托。”<br />
<br />
刘嘉俊解释韩寒后来考过的原因，是那时候他自己的车到位了，有了强大动力。然后，任务更新，韩寒想成为一名职业赛车手。人们还等着看这个小孩的文学之路能走到何处，他却早给自己换了个行当。当时他出版了《三重门》和《零下一度》两本书，几乎所有赚来的钱，他都投到了赛车上。“一开始自己水平不够，没有车队要你，你不能拿嘴巴上说，你得先向人家证明自己，在证明自己的过程当中，我就花了很多的钱。”<br />
<br />
他看起来像个多功能永动机。每年他都以一个专业车手的身份去参赛，也以相当稳定的状态出版一两本书。此外，2006年，韩寒推出了自己的唱片《十八禁》，一手包办作词、演唱及MV的创作、导演和演出。2007年全国汽车场地锦标赛，他获得了个人年度总冠军。同一年，他的博客点击过亿，一年以后，他的博客点击率成为全球最高。<br />
<br />
他一直在做自己有兴趣的事情，没有很大压力，却能够做出很好的效果。<br />
<br />
2009年，韩寒又有一项新的志业，他要自己投资，做杂志主编。经过了大半年的波折，杂志《独唱团》终于下厂印刷了，只是他本来希望在今年面世的，可能赶不上了。<br />
<br />
但对于这样一个不以断代划分作家和群体，主张个体独立价值的韩寒，杂志到底出现在2009年还是2010年，也并不值得介意。他只需要稳定地前进就可以，即便别人看来天马行空，但他知道自己的脚踏实地。<br />
<br />
不需要买房子给自己安全感<br />
<br />
休学那天，韩寒先去了班主任办公室签字，老师们试探着问他：“你不进大学，将来怎么挣钱养活自己？”<br />
<br />
17岁的韩寒直接回答：“稿费。”<br />
<br />
他当时说话并不是很有底气，但他别无选择。原先有《萌芽》、《少年文艺》的稿费小打小闹，他想着能由此不花父母的钱。<br />
<br />
结果第二年《三重门》出版，首印3000册，随即加印3万册。几乎是每隔几天，编辑就打一个电话：又加印3万。也就是每接一个电话，韩寒就知道自己账上添了3万。<br />
<br />
他本来想用版税买辆摩托车，但是面对过于频繁的3万一跳，他动了买汽车的心。这个18岁出门远行的少年背着一书包人民币，最后选了一辆富康，他嫌弃桑塔纳是上海出租车。<br />
<br />
他在杂志上看到北京有一家汽车改装店，当时汽车改装事业尚未普及，逸兴壮思的他直接开来北京改装――结果他发现，自己千挑万选了一辆北京出租车。<br />
<br />
当然也因为买车的时候他没有等到，或者预计到，一本书的版税能到200万。<br />
<br />
但韩寒还是一直喊穷。这位80后名人不讲究吃穿，一年买衣服鞋袜的钱不超过5000块；不讲究投资置业，只有一套又远又小的房子，“价钱也只有上海公寓200多万均价的一半”；唯独讲究车。买来以后，开上一年，半价出手。另外，还容易心软，人家问他借钱，基本上都答应。有朋友买房需要七八十万，不想贷款，直接问他开口，他也借了。<br />
<br />
他一年的支出大概有200多万，与他的收入基本持平。这使他有时会感慨：“我是全国作家、赛车两大领域中最顶尖几个人之一，那我在两大行业做到金字塔顶端，结果我所有收入加起来年薪也不过20多万英镑，这在世界上说出去会让人笑掉大牙。”<br />
<br />
窘迫的时候他在楼下请人吃一顿150块的饭，是他全部积蓄的一半；高兴的时候，他花三四十万买一辆摩托。这都是他成名之后的事。<br />
<br />
韩寒的杂志《独唱团》，却愿意给供稿者开出千字1000～2000的稿费，约为行业标准的10～40倍，一期杂志的稿费支出预计在50万元。自己尚未“脱贫”的韩寒，称这是为了“改善一下供稿者的生活”，因为“写字的大家混得都不容易，我算是混得好的，自然应该少赚一些，让大家多拿一些。”<br />
<br />
他觉得自己有投资的渠道与眼光，可他不喜欢做这些事。他不需要在房子上建立安全感。他的钱，要花在愿意花的地方，跟梦想有关：不仅是他自己的，也是别人的。<br />
<br />
“我没有什么大的梦想，我的所有梦想都是阶段性的，所以实施起来很容易。”他这么描述。<br />
<br />
与其说是梦想，不如说是想去做什么事，考虑之后就会去实践。这也是急脾气的韩寒的习惯。他重视的是，给其他的年轻人一些做梦的空间。“我希望我的杂志能让这些人即使住500块租来的房子，内心也能有无比坚定的安全感。”<br />
<br />
这是一个以文学为志业的人的梦想，也许，人们会将此称为理想主义，但韩寒的不同，在于他付诸行动，进而去影响他人。他要宣告的是，在这个时代，梦想同样可以是头上星空、心中律令，同样可以与占有的社会资源无关，你与我可以平等地拥有。<br />
<br />
有趣故我在<br />
<br />
一个因为聪明有趣而被公众关注的人物，常常会被忽略他所做的努力。<br />
<br />
比如车手韩寒踏踏实实的练习，比如作者韩寒每天花若干小时阅读各类资讯，虽然对他这样哪里都能发现乐趣的人来说，他的自我修炼过程也不会枯燥。就像他习惯要和领航员聊天，不管是练习还是正式比赛，当然他的注意力仍然是集中在驾驶上的，或者就像他阅读或者写作时，时不时就要吃点零食。<br />
<br />
就像你看他是个嘻嘻哈哈的主儿，立马他就给你一个正经表态：“这个问题我不能现在回答你，我要思考一下，因为我觉得这个问题比较有意思，我希望我的回答20年后看还是差不多的观点，而不是当时随便说两句。”<br />
<br />
很多时候他更喜欢隔着电脑写字，而不是当面表达，如他的博客公告里所说的，“接受少量专访，原则上不接受当面采访。”在他后来接受的电视采访中，他表现自信，但看起来仍然略有腼腆，对一个青年人来说，这似乎是种比较可爱的品质，比如他笑起来或者听人说话的样子。他的声调偏于温和，似乎也念不出他文章里的铿锵；对话间有急智，但也未及文字里那么多妙趣横生的包袱。<br />
<br />
更多的时候，他是个注意自我保护的人。邮件往返，他不喜欢的问题可以直接删除，而当面的时候好歹要敷衍一下。<br />
<br />
想起10年前的《对话》里长辫大姐的指责，当年细长苗条的瘦企鹅OICQ已经变成了戴着红围巾的大胖企鹅QQ。已经27岁的韩寒哈哈大笑：“前两天腾讯公司送了我一个很好的QQ号，这两天我开始用QQ了，我真的长大了。”<br />
<br />
他不是斤斤计较，对有趣的东西，他会本能地调侃。<br />
<br />
像《对话》里，对那些微妙的不友好，他坐在那里本身就是一种调侃。<br />
<br />
当然现在他更自信，比如答问题时信口开河，听的人感觉太玄，问他：“真的是这样吗？”<br />
<br />
“当然不是咯。我就随便一说。”<br />
<br />
如何确定他随便一说还是真心实意，他的建议是：“再问我一遍。”<br />
<br />
对他这样轻逸有趣的人，树枝之上是他的领土，我们适合看他如何在树与树之间腾挪转移，看他飞花摘叶皆可伤人。遗憾的是，10年前很多人想的是：把森林砍光，逼他证明自己能直立行走。<br />
<br />
这未免太煞风景。<br />
<br />
事实上，就算砍光森林，也未必抓得住他。<br />
<br />
韩寒：对公共事件发言是纯文学<br />
<br />
如果韩寒当了市长，他如何让他的城市不至于让生活更糟糕？<br />
<br />
只是韩寒拒绝这样的设想，他认为对公共事务发表看法，就是地道的纯文学<br />
<br />
■本刊记者/许荻晔<br />
<br />
《要是敢一人一票选举，市长会是韩寒么？》一个如此标题的帖子曾经出现在一个爱车论坛上，引发了车友的争论。最后大家的结论是，韩寒可能会差几十票排在第二，排名第一的，也许是周立波。<br />
<br />
“也许有人会说他年轻，富于批判精神却缺乏管理经验，但是我得说，他管得绝对不会比今天坏了。”有人这样回复主帖。<br />
<br />
但是韩寒拒绝这样的提议，因为他不愿意坐进开会大楼里，和“一堆”不解风情的人在一起工作。<br />
<br />
“再问一遍。”这个依然年轻但已经成熟的车手和作家这样解释如何区分他在调侃开玩笑或者说了真心话。<br />
<br />
再问一遍，他会认认真真地解释这一切，但是之前，他追求有趣，追求个性自由。<br />
<br />
不愿意和一堆不解风情的人在一起工作<br />
<br />
《新世纪周刊》：如果你被推选为市长，会愿意担任吗？<br />
<br />
韩寒：我想我不会愿意，在现阶段我不愿意和一堆不解风情的人在一起工作。<br />
<br />
《新世纪周刊》：愿意在国内当市长还是国外？希望接管大一点的市还是从小点的起步？<br />
<br />
韩寒：我觉得市长并不是接管，而是服务。市长应该像KTV小姐一样服务大家，而且要比她们做得更好，她们只服务给钱的（人），市长还要服务没钱的（人）。<br />
<br />
《新世纪周刊》：你怎么选拔你市里的公务员？<br />
<br />
韩寒：因为我没有当市长的打算和可能，所以我就不浪费脑细胞意淫了。<br />
<br />
《新世纪周刊》：如果市民要有尊严地活着的话，自身需要具备什么条件，而市长可以提供的是什么？<br />
<br />
韩寒：我想在中国作为市长，大部分人的眼睛都是盯着他的上级的，他更关心他的上级的口味和方向，至于市民，我认为他们觉得只要不饿死就行了。<br />
<br />
对公共事件发言就是纯文学<br />
<br />
《新世纪周刊》：你写一篇博客需要花多长时间？<br />
<br />
韩寒：15分钟吧。<br />
<br />
《新世纪周刊》：据说你写作一天也就写六七千字的样子，15分钟写一篇博客，真是很快啊。<br />
<br />
韩寒：是这样的，我觉得我很难得会一天写六七千字，对我来说一天写一两千字最多了。博客写得快是因为我觉得我打字的速度越来越快了，没有别的什么，（所以）15分钟到半个小时就可以写一篇。<br />
<br />
《新世纪周刊》：需要构思吗？还是直接对着电脑就开始写？<br />
<br />
韩寒：可能需要构思十几、二十秒吧。<br />
<br />
《新世纪周刊》：为什么现在这么热衷于对公共事件发言？<br />
<br />
韩寒：我其实没有考虑过这个。我觉得作为一个作者、一个作家，不写这个写什么呢，我没有想过。<br />
<br />
《新世纪周刊》：不是可以搞文学创作吗？<br />
<br />
韩寒：这个就是文学创作啊。<br />
<br />
《新世纪周刊》：因为很多人会觉得纯文学应该是怎样怎样的，但是你写的是介入现实的东西。<br />
<br />
韩寒：那因为他们是傻B喽，我写的就是纯文学，这个才是传统意义上最纯的文学。如果我们的文学是从近代开始算起的话，古代不算，从白话文开始算起，（我写的）这个就是纯文学。（如果他们觉得这个不是纯文学），那是因为他们不懂，他们以为故弄玄虚的东西才是纯文学。他们那些专家说的那些啊，忽悠一些笨蛋是可以的，但是忽悠不了我。还是那句话。<br />
<br />
《新世纪周刊》：其实你所表达的，也是一些人情常理，但是好像现在人们特别需要听到由你说出来，觉得这是为什么？<br />
<br />
韩寒：我觉得这个其实应该是别人来评价的，为什么他们喜欢听从我口中讲出来（的东西）。对我来说我可能只是一个讲述者，一个写作者，我从来没有做过任何民意调查，我的风格就是这样的，大家如果喜欢就有缘，如果不喜欢我也不勉强大家非要喜欢。所以我觉得应该问他们是怎么想的。<br />
<br />
《新世纪周刊》：越来越多人开始接受这样的一个韩寒，对此是什么感觉？<br />
<br />
韩寒：我不会有那种高兴――一朝得志、欣喜若狂那种，但是没有理由不高兴，我觉得。因为作为一个写东西的人，当然是希望自己写的东西被更多的人所看到、所喜欢，我相信任何一个写作者都会高兴的。<br />
<br />
《新世纪周刊》：你是对社会事务始终保持着关注和关怀，还是会在有些时候也觉得不想说？<br />
<br />
韩寒：是这样的，那些觉得已经看得太多了、不想再说的人可能是有别的“事”的吧，要不就是挣着钱了，要不就是反正怎么样都是挣不着钱，或者想法都放到别的方面，然后以麻木作为一个借口。但是对于我来说可能一直是一个兴趣，当然如果哪天不想写了，（其实也）不是不想写了，就是没有兴趣了。<br />
<br />
《新世纪周刊》：比如同一类的事件，看得多了，会不会也有点感情被抽空的感觉，变得麻木？<br />
<br />
韩寒：其实对于这些东西我也看得多了，我从初二的时候就开始看各种社会新闻，基本上看了10多年的社会新闻了，什么样的新闻都看到过。但是我国这个层出不穷的这个素材啊，让我永远保持着写作的这种创作欲望。<br />
<br />
《新世纪周刊》：还是不断保持新鲜感啊。<br />
<br />
韩寒：是的，我相信有些事情，像你们新闻从业者都会有这样的感叹，很多事情不光发生一遍，还会发生一百遍，而且以各种各样的很新颖的方式。<br />
<br />
《新世纪周刊》：你好像每次都会打一些比喻，用一些生活常识，把对这些事件的冠冕的描述消解掉。<br />
<br />
韩寒：因为我个人是不喜欢那种冠冕堂皇的东西的，一方面是因为大家以前也听多了，这种东西也没有说服力。另一方面是因为我是一个写“纯文学”的人，这些东西实在太不纯文学了，甚至都是垃圾，所以我觉得我要摒弃这些东西。<br />
<br />
时间会说真话<br />
<br />
《新世纪周刊》：从你休学到现在已经10年了，你在其中最大的收获是什么？<br />
<br />
韩寒：最大的收获是我开车开得很好。我其实想说的就是这个，觉得开车开得还可以。<br />
<br />
《新世纪周刊》：在学校体系内就很难达成这个目标了。<br />
<br />
韩寒：对，的确是这样。<br />
<br />
《新世纪周刊》：休学以后你会有焦虑吗？<br />
<br />
韩寒：我觉得焦虑没有，但是有的时候会有迷茫。这个我觉得很正常，我觉得是十七八岁的小孩从学校出来都会有的现象。<br />
<br />
《新世纪周刊》：那你怎么克服这些迷茫的？<br />
<br />
韩寒：我觉得不用克服。当时十七八岁，反正又不会得抑郁症，我觉得不用克服。<br />
<br />
《新世纪周刊》：随便它就过了？<br />
<br />
韩寒：因为我觉得人类总是想克服什么，但是有的时候有些东西就是这个阶段应该带来的东西，你别一心想要克服它，有的时候还不如享受它。<br />
<br />
《新世纪周刊》：我看到你在其他的访谈中说你的人生观是形成于2000年到2006年，那你对你1999年的判断是什么？<br />
<br />
韩寒：是这样，这些对我来说啊，没有一个什么界定啊，或者顺便给它们取一个象征性的意义。如果我说我的人生观是形成于1998年到2006年，你就会问我那1997年对你是怎么过的呢。所以我觉得当时我也就是那么顺口一说。<br />
<br />
你10年当中的梦想变化过吗？<br />
<br />
韩寒：10年之前我就没什么梦想。<br />
<br />
《新世纪周刊》：那你做事、做决定是什么来支持的，想到了就去做？<br />
<br />
韩寒：我觉得梦想是阶段性的，我没有什么大的梦想，但比如说这个阶段是一个月内把这本书写完，下个阶段是三天以内去玩什么。我的所有梦想都是阶段性的，所以实施起来很容易。<br />
<br />
《新世纪周刊》：你还记得你2000年做的《对话》节目吗？<br />
<br />
韩寒：（笑）前两天腾讯公司送了我一个很好的QQ号，我以前从来不用QQ的，这两天我还在用QQ，我真的长大了。我成熟了。<br />
<br />
《新世纪周刊》：做这个访谈的时候你有屈辱感吗？<br />
<br />
韩寒：还行，不大会有。<br />
<br />
《新世纪周刊》：好像现场的人都有点抵触你，看你笑话这一类的态度，感觉没有尊重作为个体的你。<br />
<br />
韩寒：不要紧，我觉得时间会说真话的。就像一个赛车手，你跟别人说我是一个很快的赛车手的时候，人家当然没有理由相信你，因为你不在赛道旁边。但如果你在赛道旁边，你有一台好车，你马上就可以证实给别人看。当时我并没有完全证实给别人看，当然现在我也不觉得自己做得能让自己满意，做得有多么好，但至少我不觉得那些人对我有多么大的恶意，或者说是一定想要说服我、鄙视我，我其实并不觉得。我相信这些人当中，现在可能会有一批人，他们会理解我，所以我对他们并没有恩怨，只怪当时自己没有能够做到理想的状态<br />
<br />
《新世纪周刊》：当时为什么会去做这样一档节目呢？你现在常常要批评央视的？<br />
<br />
韩寒：（笑）那是以后。当时刚从学校出来什么都觉得新鲜。<br />
<br />
《新世纪周刊》：电视访谈也想去做一做。<br />
<br />
韩寒：对啊，那时候连飞机都没坐过几次（笑）。<br />
<br />
《新世纪周刊》：这次节目是坐飞机去的？<br />
<br />
韩寒：我忘了。但是当时去外面就感觉比较新鲜。<br />
<br />
《新世纪周刊》：只要是去外面就愿意去尝试啊？<br />
<br />
韩寒：是，这是一方面。另外一方面我觉得，无论怎么说呢，当时是愿意去跟别人争辩的，不像现在不大愿意的，不大跟别人争辩，只是发表自己的观点而已。<br />
<br />
《新世纪周刊》：2006年不还是还跟白烨他们争得挺厉害的嘛，现在改了？<br />
<br />
韩寒：2006年的时候其实也是发表自己的观点。<br />
<br />
《新世纪周刊》：可能因为是有来有往的东西，所以大家容易把它看成是争辩。<br />
<br />
韩寒：是的。但对我来说有一个区别：以前是特别想把别人拉到跟自己一样的观点，但现在我会告诉你我不认同你，然后我的观点是叉叉叉。所谓有来有往只是说我回应他的观点我不认可，然后我的观点是什么，而不是说服他接受我的观点。那些看起来是写给那些人的文章，写给争辩时的文章，那些事实上我并不是写给他们看的，我是写给读者看的。<br />
<br />
《新世纪周刊》：你一直怀揣着这样一个预期读者的形象。<br />
<br />
韩寒：当然，这些打口水仗的文章当然（会有读者），包括当年梁实秋和鲁迅写来写去的，我相信梁实秋也不是写给鲁迅看的，鲁迅也不是写给梁实秋看的。他们两个谁都改变不了谁，都是写给各自的读者看的嘛。<br />
<br />
我有我自己的文学标准<br />
<br />
《新世纪周刊》：《独唱团》到什么进度了？<br />
<br />
韩寒：下场印刷了。<br />
<br />
《新世纪周刊》：什么时候能上市呢？<br />
<br />
韩寒：个把月吧。<br />
<br />
《新世纪周刊》：那就是说今年没有办法上市了？<br />
<br />
韩寒：无所谓了，那就明年喽。<br />
<br />
《新世纪周刊》：本来我们很期待看你画的刊号。<br />
<br />
韩寒：其实没有什么可期待的，就是一本普普通通的文艺杂志。<br />
<br />
《新世纪周刊》：大家都很期待看看韩寒选稿的眼光啊，为振兴文化人的生活所做的努力，等等。<br />
<br />
韩寒：希望是这样。但归根结底事实上这也只是一个文艺杂志而已。<br />
<br />
《新世纪周刊》：多数时候你好像是以个人的姿态出现，但有些时候你似乎不希望自己是仅仅代表个人，就像你办杂志，就不愿做小众的。为什么会有这样的情况？<br />
<br />
韩寒：我的意思就是说，因为我不是想把这个做成一个特别小众的，或者类似于粉丝团、后援团的内部刊物，所以我很多时候并不是特别个人化地去搞一些个人崇拜啊什么的。<br />
<br />
《新世纪周刊》：那种青春、校园文学你怎么定义它们的文学价值？<br />
<br />
韩寒：我也看一看的，有些故事也挺好的，有的我也挺喜欢的，不一定每篇都要去针砭时弊、借古讽今啊之类的，只是对于我来说，小说可能会涉及到这方面，但是杂文不会。<br />
<br />
《新世纪周刊》：你自己觉得你是写小说写得好还是杂文写得好？<br />
<br />
韩寒：其实我个人认为是小说。<br />
<br />
《新世纪周刊》：但好像多数人更喜欢你的杂文。<br />
<br />
韩寒：这个我觉得很正常，因为我是有我自己的标准的，他们也有他们的标准。<br />
<br />
《新世纪周刊》：你的标准是什么？文学价值吗，还是花费的心力等等？<br />
<br />
韩寒：标准是一个很难说清楚的东西，就像你觉得一个人漂亮，但她漂亮在哪儿你也说不清楚。<br />
<br />
《新世纪周刊》：你可以描述一下这种感觉啊。<br />
<br />
韩寒：对，但是真正的标准往往是一种难以描述的感觉，很难用词汇形容出来。<br />
<br />
每个人都是演员<br />
<br />
《新世纪周刊》：电驴、BT被封对你的影响大吗？<br />
<br />
韩寒：还好，因为我个人不是特别喜欢从网络上下东西。因为我接受不了，我喜欢的东西至少必须是在一分钟内完成，不然我会很着急。<br />
<br />
《新世纪周刊》：有感到自己会无能为力的时候吗，软弱啊这种？因为看你一直都是很积极。<br />
<br />
韩寒：那当然，人类都会有这样的时候。<br />
<br />
《新世纪周刊》：也是顺应着去接受吗？<br />
<br />
韩寒：那只能这样喽，如果实在不喜欢，特别不喜欢这个状态你就去克服它。<br />
<br />
《新世纪周刊》：比如说呢？<br />
<br />
韩寒：每个方式不尽相同。比如说这件事情让你无能为力你就去解决这件事情喽，如果你解决不了这件事情你就认输，或者再去做另外一件事情，我觉得是这样的。因为有些感觉让你不爽的话你就去克服它，有些感觉虽然它是个贬义词，但是没有让你不爽的话就不需要去克服它。<br />
<br />
《新世纪周刊》：你觉得人的什么品质最重要？比如你愿意和这样的人交往？<br />
<br />
韩寒：我觉得这个吧，我现在说的都不作数，因为交朋友、娶老婆都得具体问题具体分析，因为你之前定的标准后来发现你在交朋友的时候不起多大作用。<br />
<br />
《新世纪周刊》：那有没有会觉得某个人的某种品质特别珍贵？<br />
<br />
韩寒：是这样，我觉得每个人都是演员，他们都会有不同程度的表演，但每个人性格表露出来的是不一样的，所以我并不觉得每个人身上有种固定的品质。<br />
<br />
《新世纪周刊》：你觉得你现在的生活方式是比较奢侈的吗？<br />
<br />
韩寒：为什么？<br />
<br />
《新世纪周刊》：因为很多人就算是有梦想，他们没有办法去付诸实践。<br />
<br />
韩寒：反正我自己觉得还是挺满意的，但是对于别人我就管不了那么多了。<br />
<br />
《新世纪周刊》：但是在你的杂志宣言中不是挺有坚持地说普通人也应该有梦想，不应该被房子束缚，等等。<br />
<br />
韩寒：对，这只是我觉得应该是这样，但如果非得要过得没有梦想，那我也没有办法，我也没有能力去干预你。<br />
<br />
《新世纪周刊》：可是你好像在努力提供另外一种可能性给大家看，能这样理解吗？<br />
<br />
韩寒：其实这个可能性归根到底，人需要的东西是一样的，只是你不能说你是呼吸氧气的，而我怎么证实给你看我呼吸二氧化碳也能活，到最终人需要的一样，也都是氧气，只是呼吸的方式和方法不同而已。<br />
<br />
《新世纪周刊》：我看到你在访谈中说过你最爱从事的职业是房地产，这个是认真的还是随便说的？<br />
<br />
韩寒：这个当然是随便说的喽。<br />
<br />
《新世纪周刊》：怎么分辨你哪些是随便说的，哪些是认真说的?<br />
<br />
韩寒：再问（我）一遍。<br />
<br />
《新世纪周刊》：你小时候真的立志长大了要做清官，打倒贪官吗？<br />
<br />
韩寒：五六岁的时候吧。<br />
<br />
《新世纪周刊》：那时候还想参与进某种秩序，然后铲除一些你认为不正常的东西？<br />
<br />
韩寒：其实小孩子没想那么多，你如果看了黑猫警长，你也就说想当个警察，每个人都会有自己的一些小小的理想。我觉得当时对于我来说并不是想当个官，当时可能是我说要做个好人，然后惩罚坏人。<br />
<br />
《新世纪周刊》：不是这样的表述，但有这样的意思。<br />
<br />
韩寒：对，当时是看了一个什么当官的电视剧，然后顺口一说。但当时对所谓的当官其实是没有了解的。<br />
<br />
《新世纪周刊》：你觉得你是适合这个时代的人吗，还是说无论在哪个时代都能获得这样的成功？<br />
<br />
韩寒：我觉得这种问题很简单。就像你问你适合不适合你男朋友呢，就得问你男朋友适合不适合。我适合不适合这个时代呢，我觉得你去问时代，不应该来问我，这个时代应该回答的问题。<br />
<br />
《新世纪周刊》：你觉得韩寒之所以为韩寒，跟上海的关系大吗？<br />
<br />
韩寒：我觉得不大，上海也是为我提供素材的地方而已。<br />
<br />
《新世纪周刊》：那北京呢？<br />
<br />
韩寒：我觉得也不大。<br />
<br />
《新世纪周刊》：北京不是让你遭受了很多不快的地方吗？<br />
<br />
韩寒：其实也没有，我说得有点夸张了而已。影响对我来说都是人给我的影响，一个城市能给我什么影响，都是钢筋水泥的。<br />
<br />
《新世纪周刊》：退学到现在，有没有发生一些人生观遭受重大转变的事情？<br />
<br />
韩寒：这个不大会，因为往往是属于那种见识比较少的人人生观容易受到很大改变。<br />
<br />
《新世纪周刊》：你一直都是见多识广吗？<br />
<br />
韩寒：也不是见多识广。就是那些人可能的确是他们一方面见的也少，另一方面看书看的也少，好不容易看本书了就都是感触，好不容易碰到个什么事就改变他们的人生观，但我觉得这样的人往往不可能成为一个特别好的作者，哪有一天到晚人生观改变的作者。<br />
<br />
《新世纪周刊》：成长过程中有没有一些事对你影响特别大的？<br />
<br />
韩寒：我觉得有，而且是很多方面，所谓的影响很大可能是心情上的影响、各方面的影响，但是我觉得这些影响只属于我个人。<br />
