《新世纪周刊》年度人物提名<br />
<br />
这些人会让我们想起2009，尽管他们没有最终取代韩寒，成为今年的年度人物<br />
<br />
陈及时、何东旭、方招――大勇<br />
<br />
这三个因抢救落水儿童而遇难的湖北大学生，做了一件他们和他们的同学认为是理所应当的事。不成熟的生命教育，少数武断的媒体，逐利的捞尸者，似乎没有人犯下太大的过错，然而人性尊严这条底线，就被狠狠地越过了。<br />
<br />
高锟――单纯<br />
<br />
只有孩童般的单纯才能改变这个世界。从霞飞路到圣约瑟书院，再从伦敦大学到贝尔实验室，何其幸运，这条路保护了高锟对科学的单纯。单，就是一。高锟这辈子，只爱了一个女人，做了一件事，得了一项诺贝尔奖。老年痴呆后，世界依旧使用着得益于他的手机通讯，这个女人依旧陪伴在他的身旁。而诺贝尔奖，真的不重要了。<br />
<br />
艾未未――硬汉<br />
<br />
汶川地震一年了。这个以艺术为业的人，坚持寻找着一个个死难学生的名字，并愿意因此面对无穷无尽的麻烦。除了一部不能公映的电影，艺术家艾未未今年并无新作问世。在力量强大的时间洪流中，有时候坚持旧的，比开创新的更加重要。<br />
<br />
季羡林――沉默<br />
<br />
大概是把想说的都说了。所以在人生的最后，他沉默地离去。抗议了多次的“国学大师”帽子，最后还是扣在了他的头上。<br />
<br />
许多年前，当他还是清华园里的年轻人时，无聊的考试、狗屁的讲义和抄得手痛的论文都曾让他在日记里大发牢骚。活得远远超过了自己的预算，对众生的相，看得透透彻彻，反而鼓舞时少，叹息时多。<br />
<br />
薄熙来、汪洋――改变<br />
<br />
无论如何，重庆需要做一些改变，薄熙来在重庆掀起了打黑风暴，原公安局副局长文强涉黑被拿下，薄熙来的下一个任务是发展经济，为此找来了他的前任，比他还年轻几岁的汪洋，后者现在在广东做省委书记。薄熙来的邀请不可拒绝，汪洋带了500亿投资重回重庆，两人亲切握手，广东和重庆的结盟，对未来十年中国的影响不可低估。<br />
<br />
龙应台――遗忘<br />
<br />
遗忘掉记忆中的沟壑，遗忘掉人造的分类标准，才能无障碍地拥抱，发现我们都是大江大海中的小小芥子，没有谁能看清波涛的方向。龙应台选择遗忘一些东西，而我们选择遗忘另一些。《大江大海1949》在互联网上悄悄传播，这是龙应台继《野火集》后最重要的一本书。她抛掉了《野火集》时代的怒目金刚，采取柔软的姿态，反而更加深入了人生和历史的底层。回应无声，文本长存。<br />
<br />
钟南山――良知<br />
<br />
除了低头做事，钟南山院士还会抬头发言。在抗击“非典”中声名鹊起的钟南山，兼具科学家与公民的素养。作为一个科学家，他的表达总是简单清晰。甲流死亡数字，我不信。甲流第二波，肯定有。甲流到底有多严重？90%流感是甲流。<br />
<br />
作为一个公民，他对政府大胆发问，有理有据，锲而不舍。钟院士老则老矣，还肌肉发达，面貌英俊，每当钟院士操着广式普通话一脸严肃地出现在电视屏幕上时，许多大妈都会放下手里正洗的碗筷、正织的毛活。<br />
<br />
唐福珍、孙中界、邓玉娇――反抗者<br />
<br />
弱者的反抗，只能依赖法律的公正。若是求公正而不得，就只得把身体当作最后的成本。到了那时，就进入了兽的逻辑、血的交易。多么幸运，孙中界在剁掉了一截手指后，引起了维权斗士郝劲松的关注，帮助他以舆论的力量取回了公道。<br />
<br />
多么不幸，唐福珍为了给女儿留下一点生存和发展的资本，不得不把自己燃烧成灰烬。邓玉娇反抗强奸，杀死了镇干部，万幸最后获得缓刑。<br />
<br />
经受苦难者其实并不生活在社会的最底层。开车的人，都有可能成为孙中界；有房之家，都可能遭遇唐福珍；邓玉娇事件，震惊每一个有女儿的父亲。<br />
<br />
房奴――蜗居<br />
<br />
就像蜗牛背着重重的壳，房奴们背着沉沉的房产。热门电视剧《蜗居》中，一个房子的梦毁掉了郭海萍和郭海藻姐妹的爱情和人生。这部电视剧的大热，体现了众多网民的感同身受。<br />
<br />
身为房奴的这一代，正是要对中国的未来负责的一代。二三十年的预期房贷，每时每刻磨损着他们对于未来美好的想象。不敢做梦，成了当代中国年轻人最大的悲哀。年轻人想象中的未来不美好，中国社会的未来也不会美好。<br />
<br />
蜗牛的悲哀就是，没有了房子，他们就再也硬不起来了。<br />
<br />
中国足协――幽默<br />
<br />
中国足坛的赌球惊动了中央，国务院特别派了一个工作组找了几名国脚谈话，问他们对中国足坛的感受，中国球员打假球早已经不是秘密了，今年最令人震惊的黑幕是，关键场可以买，冠军可以买，俱乐部老板大庄家小庄家守门员中后卫大家团结起来欺骗球迷，亏得那么多痴心人还为一场没胜的比赛落下那么多伤心的泪水。<br />
<br />
更可怕的是他们似乎不仅赌国内联赛，有人举出了当年国足客场踢平卡塔尔后皆大欢喜的那个场景，认为那场球肯定有问题。<br />
