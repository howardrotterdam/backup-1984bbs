 <br />
最近看到了几条关于上海的新闻，联系在一起非常有意义，首先，上海的土方车以几乎平均每天随机压死一个人的效率推进着城市的建设，照这样下去，中国馆就是屁民的鲜血染红的。 <br />
<br />
其次，上海市政法委书记宣布，坚决不让黑势力在上海市立足。这点我是相当放心的，因为上海市的物价那么贵，没有哪个黑社会老大能养得起小弟，上海已经从根本上杜绝了黑社会产生的土壤。<br />
<br />
再者，上海市政府又宣布，在必须时候会实施单双号限行等措施。这点是向北京政府学习的。北京市早先在奥运会期间实施单双号，但是北京上牌只要两百元，而且没有道路通行费。在上海市通行的上海牌照的汽车，有三万到四万元不等的牌照费，另外，上海的汽油价格也要比其他省市贵3毛到7毛钱，收了钱不光不办事，还要办了交钱的，你让那些收了钱还要办事的黑社会怎么生存嘛。对了，还有1800元的道路贷款费要交，这就很奇怪了，首付是我们付的，贷款是我们还的，利润全都归你了，最后连摸一下还得定时啊。<br />
<br />
虽然这个规定对我没有什么影响，因为我平时都在乡下，很少进城，但是如果到了那天，真要限行，我觉得大家除了直接把车停在马路上回家以外，也没有什么办法，政府可以因为道路太堵，所以限制汽车上路，而我们不能因为领导太蠢，而限制他们上班。<br />
<br />
另外，有很多的朋友问我关于谷歌要退出中国有什么感想。在谷歌图书馆扫描中国作家作品一事的时候，就有记者问我，GOOGLE未经你的同意，扫描了你的书放在网上供人免费看，说大不了赔你60美金完事，问我怎么看。我说，如果他真这么干了，那就能解释为什么他的中国市场份额做不到第一了。回到家一上网我才弄明白，原来只是扫描了我的书一个目录而已。于是，我才明白为什么他的市场份额做不到第一了，搞他的人太多了。其实，无论谷歌是真退出假退出，我都表示非常的理解，我唯独不能理解的是，有个网站的调查，有7成的网民表示，不支持谷歌对中国政府提出的要求对审查结果不设置屏蔽审查的要求。在看一些政府官网的投票结果的时候，你经常觉得自己为什么永远是在民意的对立面上，看久了甚至觉得自己是个90后，怎么从来都是非主流。其实这些网站才是急需屏蔽的。我能够容忍把黑的说成是灰的，把白的说成是米的，但决不容忍颠倒黑白。<br />
<br />
谷歌如果离开中国，最扼腕痛惜的应该是一些作家，当然，不是因为中国作家代表了社会的良知和进步的力量，他们从来不关心言论的限制，就算文化部门把中国汉字中的一大半都屏蔽了，他们也有本事在剩下的汉字里接着歌功颂德，他们痛心的是，早知道你要跑，当时就收下你那60美元了，我相信这应该会是大部分中国作家在电子版权上的第一笔收入。不就是想多要40嘛。<br />
<br />
最后，我看到一个新闻说，以后手机上转发黄段子或者黄色信息将停止该手机短信功能，必须到公安局写下保证书才能开通短信功能。政府就是这样，他永远给你一个动词和名词，然后永远不解释这个名词，比如说，不能反革命，但从来不告诉你什么是反革命，不能犯流氓罪，但从来不告诉你什么是流氓罪，这次是不能发黄段子，但是从来不告诉你什么是黄段子。我本将心照政府，奈何政府没标准，导致了有些朋友莫名其妙就触到了雷区，甚至有些五毛党都经常陷于拍个马屁结果审核没通过的尴尬。我的提议是，对于雷区，就应该写清楚，这一片是雷区，你进去了就后果自负，你不光没有明确标明，反而时不时人行道上埋个雷，炸了算谁的呢？鉴于快到新年，为了避免广大网友在转发各种短信的时候不幸被停机，导致年初一进派出所写保证书的悲剧发生，我决定牺牲自己，在这几天里，源源不断的转发各种黄段子，直到我的手机被停机了，我再上来转告大家，到底什么叫做黄段子或者黄色信息。所以，最近接收到我的黄色段子或者色情短信的朋友们，请不要误会，我不是在发春也不是要调情，我只是在探索。</div>

							
							
							
							
