													<h2>芦笛：老邓为何决定开枪？――兼论老邓的其他选择</h2>
						
						
																											<div id="postmessage_360413" class="t_msgfont"><a href="http://www.bullogger.com/blogs/ludiblog/archives/348964.aspx" target="_blank">http://www.bullogger.com/blogs/ludiblog/archives/348964.aspx</a><br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; 老邓为何决定开枪？<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;――兼论老邓的其他选择 <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;芦笛 <br />
<br />
&nbsp; &nbsp; 前两天还和朋友谈起这事，现在布朗运动网友又引发了讨论，那就再说说我的看法吧。 <br />
<br />
&nbsp; &nbsp; 已经说过，认为六四镇压合理的人分两大派，一派是“法理派”，即认为学运是犯法的，任何主权政府都有权镇压。另一派则是“功利派”，认为镇压换来了稳定和后来的繁荣。大多数人都持后一类态度。有的人则兼而有之，认为镇压既是合法的，在功利上也是合理的。 <br />
<br />
&nbsp; &nbsp; 这两种主张我都详尽批驳过。对法理派，我告诉他们学生和政府都犯了法，但学生是轻微的offence，政府则是犯了大罪：在戒严法制定出来之前几年便无据执法，宣布戒严；欺骗公众罪；屠杀平民罪；以及军事政变罪（非法软禁党总书记，罢免人大常委等等）。 <br />
<br />
&nbsp; &nbsp; 对功利派，我告诉他们屠杀非但没有解决党内危机，反倒使得党彻底失去民心，内外交困，遭受国际社会一致声讨制裁，经济凋敝，民不聊生。当时若有外敌入侵，则我党立即垮台。危机是慢慢拖过去的。至于后来的经济繁荣则是90年代东南亚发生经济危机，港澳台资本家为了逃避危机，大量将工厂内迁引出的，与大屠杀什么关系都没有。把先发生的大屠杀当成后发生的经济繁荣的结果，乃是典型的国产姨爹思维。实际上，大屠杀给中国带来的损失在各方面都是惨痛的。它吓得我党肝胆俱裂，使得朝野良性互动再无可能，政治改革中断，制造出了如今的庞大的权贵资产阶级以及无法解决的社会问题。 <br />
<br />
&nbsp; &nbsp;这都是说过几万次的车轱辘话，再说真成祥林嫂了。这次只想说过去没怎么细说的问题：邓小平为何大开杀戒？ <br />
<br />
&nbsp; &nbsp; 法理派与功利派的共同假设，都是“若是不开枪，则政府就会被推翻”，而且都以西方政府为论证出发点，认为那是“人类社会常识”，却忘记了人类社会有各种各样的，他们的社会不是我们的社会，他们的政府不是我们的政府，他们的正义，呃，当然也不是我党的正义。在西方，如果闹出八九那么大的“风波”来，政府确实会垮台。可惜啊，那是他们的政府，不是我们的政府。 <br />
<br />
&nbsp; &nbsp; 已经反复说过了，民意在中国的作用即使不等于零，也是趋近于零。我党是一个自我支持的官僚集团，并不需要人民去拥护它。民意只在党内分裂时会被党内野心家用作权力斗争借口。我党真要垮，也只会是因为大规模内讧所致，绝不会被人民推翻。而且，哪怕是党内分裂也不要紧，只要党魁牢牢捏住枪杆子，他的统治也不会被推翻，文革已经充分演示过这一点了。认为八九年政府会被人民推翻，犹如造反派领袖认为他们真是靠人民群众“夺了权”一般，都是自我恭维过份。其实都是我党拜民教的流毒。 <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;既然政府不会被人民推翻，老邓还为何杀人？是毛式paranoid的过度病态反应，还是别有用心在焉？ <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;已经说过了，老邓决定杀人，乃是杀鸡训猴，项庄舞剑，其意常在沛公。那猴，就是党内赵紫阳为首的改革派，那鸡则是倒在长街上的百姓。死难者不过是老邓为了巩固党内团结付出的“必要开支”而已。他不是因为怕人民推翻他才开枪，而是借头示众，杀人立威，制造一个大血案慑服全党，告诉他们无路可退，只能跟自己干到底。这就是为水泊梁山发明、为列宁斯大林毛泽东发扬光大的伟大的“投名状”战略。 <br />
<br />
&nbsp; &nbsp; 八九年中国的确遇到了严峻危机，不过那危机不在党外而在党内。从一开始，改革就是不得民心、不得党心的，完全是老邓一人逆潮流而动，违反党内外绝大多数人的意愿（除了少数开明知识分子之外）搞起来的。他靠实力把胡赵提上去，弄了个中顾委，把那伙被毛泽东腊制多年的榆木脑袋们统统弄进去架空了，让胡赵掌握了具体执行权，自己垂帘听政。胡赵虽然拉起了自己的体系，但一直是党内大佬的眼中钉。86年学运，大佬们以此为理由打下了胡耀邦，把李鹏、姚依林等人推上去。赵紫阳接任总书记后，地位一直不稳。我记得早在88年，消息灵通的香港《争鸣》杂志上就多次发表他地位不稳，可能会下台的消息。 <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;89学运使得老赵的处境雪上加霜，进退两难。作为对意识形态负责的总书记，他很可能步胡耀邦后尘，被保守派大佬们指为“自由化”罪魁祸首；如果他主张强硬镇压学生，那未必会赢得大佬们的欢心，反倒让他丧失可以利用的政治资源。在这种情况下，他决定背水一战，不但在5月3日发表的纪念五四的讲话中不提“反自由化”，而且还说学生的要求与党的一致。在5月4日召开的亚银会议上，他还公开与“四二六社论”唱反调，说学潮会逐渐平息，中国不会出现大的动乱，以获取学生欢心。 <br />
<br />
&nbsp; &nbsp; 这也倒罢了。最要命的是，他大概是听了姨爹高参的劝告，居然在5月16日会见戈尔巴乔夫时公开出卖了邓小平，告诉中外记者他其实做不了主，掌舵的不是他而是邓小平。这消息立即传遍海内外，引起强烈反响（注：赵在其访谈录里说那是在亚银会议上说的，这与我记忆不符。我牢牢记得国外广播这一消息是在他会见戈尔巴乔夫之时，中文维基百科也如是说）。 <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;此乃老赵的致命失误。我当时听到这新闻，连连跌足骂姨爹，知道老赵就此完蛋了。伟大领袖毛主席教导我们：“我们的权力是谁给的？不是人民给的，而是主子给的。”（为本人无耻篡改）。老赵唯一可以依靠的力量不是人民，而是他的主子邓小平。可他见不及此，却以为可以像毛泽东那样，利用人民打代理战争，把人民的斗争矛头引到老邓头上去，借力打力，摆脱太上皇，当个真正的天子，潇洒治一回。 <br />
<br />
&nbsp; &nbsp; 老赵大概看够了文革中的代理战争，居然会姨爹到去相信我党拜民教，以为可以用人民去当杀人刀，却不知道第一，人民在权力斗争中毫无用处，在党内角力靠的是实力，不是人民是否拥护你。第二，人民乃是多头怪兽，除了列宁党，谁都无法控制。而六四乃是自发群众运动，属于毫无理性的多头抽风。老赵根本不可能让学生令行禁止，配合自己。所以，伟大领袖毛主席教导我们：“真正的铜墙铁壁是什么？是群众，是千百万真心实意拥护革命的群众。勿过，那是一堵贾蔷，靠上去后你才发现它根本不存在。”（关于贾蔷的笑话，请参考《红楼梦》） <br />
<br />
&nbsp; &nbsp; 这不幸就是老赵的痛苦发现。他让阎明复去游说柴玲、吾尔开希那伙烂仔，让他们撤出广场，说到几乎声泪俱下，却什么鸟用处都没有。漫说柴玲那“民主克格勃”正在抓“特务”和“学奸”，谁敢去跟党内政客勾结，“出卖学运”？就算是连柴玲也接受了“招安”，那他们也只会立即被更激进的学生领袖罢免。自发的群众运动根本就是不可控的。老赵不知道这点，乃是因为他终生从事的历来是为列宁党严密操控的群众运动。 <br />
<br />
&nbsp; &nbsp; 假墙既然靠不住，那还凭什么去跟老邓斗？翻出荷包底来，无非也就只有体改所那几个姨爹罢了。5月17日，严家其、包遵信等发表声明，公开攻击邓小平，在赵紫阳的棺材上钉上了最后一枚钉子。5月19日凌晨，赵紫阳突赴天安门广场，看望绝食学生。在我看来，此举涵义很明白：他情知镇压在即，自己行将完蛋，趁自己尚有行动自由，给学生一个含混的警告，劝他们撤出广场，同时为历史留个证据。 <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;老赵的背叛让老邓怒不可遏，下了铁腕镇压学运的决心。共产党人最痛恨的恶德就是背叛，而在老邓眼中，老赵就是背恩反噬的叛徒（这其实也错不到哪儿去，老赵确实是他一手提拔起来的）。把他抓起来倒是小菜一碟，问题在于当时党内四分五裂，非但舆论对学运持一边倒的同情态度，而且在李鹏于5月20日颁布戒严令后，就连老帅们也表示：“军队和老百姓，咱们是一家人，唉嗨咱们是一家人哪！”认为不能用军队去镇压学生。这就更激发了老邓的杀心。他需要一个大屠杀慑服全党，重建他说一不二的威望。 <br />
<br />
&nbsp; &nbsp; 这是共党的思维定式，他们深知贱民心理，知道要让全党全军全国各族人民真心拥戴你，先得让他们怕你。所谓“敬畏”，畏是因，敬是果。伟大领袖毛主席把这点参悟得很透彻，在建国之初便英明指示：“要杀掉一批人，不杀政权不能巩固。但要杀得有理有据有节，让各界无话说。”敬爱的春桥同志也曾指示：“如何巩固政权：杀人。”因此，老邓动了杀机，乃是无耻阶级革命家们“突出重围”、解决难题的典型便捷手段。 <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;可惜从权术的眼光来看，老邓不及老毛多矣，他忘记了老毛那后半截话“但要杀得有理有据有节，让各界无话说”。学运的全过程都让海内外人民看得清清楚楚，他还有那本事硬说“一小撮反革命暴徒发动反革命暴乱”。于是非但没有达到原设计效果，反倒弄得政府彻底失去民心，党内四分五裂，危机不是解决而是深化了。更糟糕的是，打掉赵紫阳引来了保守派复辟的趋势。六四后，《人民被日报海外版》读来如同文革出版物。此后国家长期陷入“鸟笼经济”的困境中，达到了国内外还债高峰。老邓的个人威望也并未得到恢复。江泽民、李鹏根本不把他放在眼里，公开在报上发表《问一声是姓社还是姓资》，就连经济改革都面临流产之虞。这才逼得他效法毛泽东“以农村包围城市，以诸侯压制北京”，发表南巡讲话，威胁“谁不改革谁下台”，把朱F基推上去作总理，把李鹏踢到人大去，这才总算勉强保住了经济改革。如果老邓92年前就死了，则今日中国肯定还在那困境中。 <br />
<br />
&nbsp; &nbsp; 所以，从功利的角度来看，老赵和老邓都犯了错误。老赵要扮演开明派没关系，但不能出卖老邓。老邓则即使被老赵出卖，也根本用不着采用大屠杀这种极端手段。 <br />
<br />
&nbsp; &nbsp; 这事若摊在老毛手上，分分钟搞掂。他根本不必下那戒严令，只要发挥“革命的灵活性”，来个华丽转身，公开表态说，学生提出的反官倒是帮助我党整风的，对学生的善意表示感谢，鼓励他们揭发党内的贪腐，然后由纪检会宣布赵紫阳涉嫌包庇家属搞官倒，实行“双规”，再来一个陈希同式的show trial，即可一劳永逸地搞臭老赵。同时严格约束保守派，不许他们再去刺激学生，则学潮迟早要平息下来。等到事态冷却后，再在学校里加强“爱国主义教育”，把学领和外国人接触、进餐的秘密录像抛出来，打成间谍，关进大牢就完了。到时候墙倒众人推，学生们只会忙着洗清自己，哪还敢再生事？ <br />
<br />
&nbsp; &nbsp; 在我看来，此策完全可行。老赵的政敌，无非是党内保守派大佬。那些人之所以要刺激学生，煽起学运，目的就是以此打倒老赵。只要老邓告诉他们，倒赵没问题，但不能再去刺激学生，则那伙人完全是可以约束下来的。另一方面，用真实或虚构的经济犯罪去搞倒老赵，不但能迎合民意，“平了民愤”，而且比政治理由更有持久杀伤力，老赵连翻案的可能性都没有。老邓计不出此，却去以大屠杀的笨拙方式倒赵，非但是杀鸡用牛刀，而且反而成全了老赵“真正的人民领袖”的历史形象，可谓愚蠢至极。</div>

							
							
							
							
