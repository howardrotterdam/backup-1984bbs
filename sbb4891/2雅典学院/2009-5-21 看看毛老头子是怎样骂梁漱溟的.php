<br />
　　梁漱溟反动透顶，他就是不承认，他说他美得很。他跟傅作义先生不同。傅先生公开承认自己反动透顶，但是傅先生在和平解放北京时为人民立了功。你梁漱溟的功在哪里？你一生一世对人民有什么功？一丝也没有，一毫也没有。而你却把自己描写成了不起的天下第一美人，比西施还美，比王昭君还美，还比得上杨贵妃。<br />
<br />
