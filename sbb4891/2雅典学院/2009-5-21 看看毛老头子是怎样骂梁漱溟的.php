　　有一些人，直到现在，反对帝国主义的话他还可以说，反对蒋介石的话，死也不肯说出来。在报纸上，在公开的言论中，他就不敢讲过去，对于过去还有一面之情。这样的人，我看相当有几个。<br />
<br />
　　爱国主义有三种：一种是真爱国主义，一种是假爱国主义，一种是半真半假、动摇的爱国主义。各人心中有数，梁漱溟的心中也是有数的。真正同帝国主义和台湾方面断绝关系的，不管他怎样落后，我们也欢迎。这一类是真爱国主义。假爱国主义，外面装得那么隐蔽，里头是另一套。还有一种，是动摇分子，半真半假，看势办事。如果第三次世界大战不打，蒋介石不来，那末，就跟GCD走下去。如果第三次世界大战打起来，他就另打主意。多数人是那一种呢？多数人是真爱国主义。几年来，真爱国主义多了起来；半真半假的，有一小部分；假爱国主义是很少的，但是有。这个分析究竟恰当不恰当，大家可以研究。<br />
<br />
　　（八）我认为梁漱溟应当做一件工作。这件工作不是由他“代表农民”向人民政府“呼吁解放”，而是由他交代清楚他的反人民的反动思想的历史发展过程。他过去是怎样代表地主反共反人民的，现在又如何由代表地主的立场转到“代表农民”的立场上来了，他能说明这个变化过程，并使人们信服，那时方能确定究竟应当把他归入那一类。他给我的印象是：他是从来不考虑他的反动立场的。但我建议，为着治病救人，应当给他一个反省的时间，并把这件事交给政协去做，此次不做结论。<br />
<br />
　　（九）“羞恶之心，人皆有之”，人不害羞，事情就难办了。说梁先生对于农民问题的见解比GCD还高明，有谁相信呢？班门弄斧。比如说，“毛/泽/东比梅兰芳还会做戏，比志愿军还会挖坑道，或者说比空军英雄赵宝桐还会驾飞机”，这岂不是不识羞耻到了极点吗？所以梁先生提出的问题，是一个正经的问题，又是一个不正经的问题，很有些滑稽意味。他说他比GCD更能代表农民，难道还不滑稽吗？<br />
<br />
　　出了这么多的“农民代表”，究竟是代表谁呢？是不是代表农民的呢？我看不像，农民看也不像。他们是代表地主阶级的，是帮地主阶级忙的。其中最突出的，花言巧语的，实际上帮助敌人的，是梁漱溟。其他有些人是思想糊涂，说了一些糊涂话，但他们还是爱国主义者，他们的心还是为了中国，这是一类。梁漱溟是另一类。还有跟梁漱溟差不多的人，冒充“农民代表”。冒充的事，实际上是有的，现在就碰到了。那些人有狐狸尾巴，大家会看得出来的。孙猴子七十二变，有一个困难，就是尾巴不好变。他变成一座庙，把尾巴变作旗杆，结果被杨二郎看出来了。从什么地方看出来的呢？就是从那个尾巴上看出来的。实际上有这样一类人，不管他怎样伪装，他的尾巴是藏不住的。<br />
<br />
　　梁漱溟是野心家，是伪君子。他不问政治是假的，不想做官也是假的。他搞所谓“乡村建设”，有什么“乡村建设”呀？是地主建设，是乡村破坏，是国家灭亡！<br />
<br />
　　（十）和他这个人打交道，是不能认真的。和他是永远谈不清任何一个问题的，他没有逻辑，只会胡扯。因此，我提议移交政协双周座谈会去讨论这个问题，同时我又要警告诸位，切记不可以认为真正有解决问题的希望。决不可能的，结果还是“议而不决，决而不行，无结果而散”。虽然如此，我还是劝大家举行双周座谈会试一试看，这比“派两个人”去听他说教要强。</div>

							
							
							
							
