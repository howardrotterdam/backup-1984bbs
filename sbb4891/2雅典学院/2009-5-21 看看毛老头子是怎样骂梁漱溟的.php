　　（一）梁漱溟先生是不是“有骨气的人”？他在和平谈判中演了什么角色？<br />
<br />
　　梁先生自称是“有骨气的人”，香港的反动报纸也说梁先生是大陆上“最有骨气的人”，台湾的广播也对你大捧。你究竟有没有“骨气”？如果你是一个有“骨气”的人，那就把你的历史，去过怎样反共反人民，怎样用笔杆子杀人，跟韩复榘、张东荪、陈立夫、张群究竟是什么关系，向大家交代嘛！他们都是你的密切朋友，我就没有这么多朋友，他们那样高兴你，骂我是“土匪”，称你是先生！我就怀疑。你这个人是那一党那一派！不仅我怀疑，还有许多人怀疑。<br />
<br />
　　从周总理刚才的发言中，大家可以看出，在我们同国民党两次和平谈判的紧要关头，梁先生的立场是完全帮助蒋介石的。蒋介石同意和平谈判是假的。今天在座的还有来北京和谈的代表，他们都知道蒋介石的“和平”到底是真的还是假的。<br />
<br />
