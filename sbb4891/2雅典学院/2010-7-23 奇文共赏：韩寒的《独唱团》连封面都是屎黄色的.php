													<h2>奇文共赏：韩寒的《独唱团》连封面都是屎黄色的</h2>
						
						
																											<div id="postmessage_754848" class="t_msgfont">2010-07-13 10:58:58 燕赵都市网 <a href="http://www.yzdsb.com.cn" target="_blank">www.yzdsb.com.cn</a><br />
　　最近这些天，所谓“青年作家”韩寒主编的《独唱团》开始上市发行，号称首印50万册“绝不掺假”云。《独唱团》的诞生虽然一波三折，但对它本身，我一点兴趣都没有，尽管我不太赞成政府过紧的文艺审查。只因一上网、一看报就时不时蹦出什么“热卖”的新闻，让人躲避无从，实在讨厌，正好又遇到全书电子版，于是浏览一遍，不禁哑然失笑：――原来不是千呼万唤始出来，而是千呼万唤“屎”出来。就连封面都是屎黄的。<br />
<br />
　　在开卷中，韩寒写了这样一段文字：<br />
<br />
　　“无论现实如何，我们总是要怀有理想的。写作者最快乐的事情就是让作品不像现实那样到处遗憾，阅读者最快乐的事情就是用眼睛摸一摸自己的理想。世界是这样的现实，但我们都拥有处置自己的权利，愿这个东西化为蛀纸的时候，你还能回忆起自己当年冒险的旅程。”<br />
<br />
　　从作者名单中，我们可以看到，什么罗永浩、蔡康永、彭浩翔、林少华、石康之流，有的是港台综艺人物，有的是京城文坛爆发户，有的是唱歌的，有的是画漫画的，有的是6岁娃娃，有的是无名女卒……先天上，就注定这种杂志格调不高、内涵苍白。换句话说，他们正是张一一所说的“乌合之众”。试看这些在韩寒眼里的所谓“佳作”，小说内容不出“火车”、“摩托车”、“青春”、“玫瑰”、“小姐”、“学生”、“男孩”的范围，散文、评论用词不出什么“纠结”、“黑色”、“忧郁”、“逼”、“傻逼”、“浪子”、“年轻时代”的滥调，不说“战争片”，就连“文艺片”也不入流，居然还能称得上“冒险”，还要等老了再去欣慰的“回忆”，天哪，如果是我，我真要为当年的幼稚愧死！这种幼稚，如果还是二十岁的青涩年代，那都还好说；可是，都快三十岁了，就实在说不过去了。白居易诗云：“莫道三十是年少，百岁三分已一分”。最惊爆的是，这些作者中，有的人都四十多岁了，都还那么能装出一张嫩脸，我这“80后”看了，在“佩服”的同时，真要为他们感到难过也。<br />
<br />
　　更妙的是，一些所谓的时评人、文化人士居然大快朵颐，什么“公民读本”、“回归常识” 、“独立思考”、“一本引领文化潮流的青年杂志”，肉麻兮兮，全是妄语，真可谓众“妄”所归。例如，有个叫“五岳散人”的网络名人的写文章说：“韩寒的杂志就该卖成这个样子，这是我对于这事儿的一个基本判读。除了韩寒本身的知名度、话题性之外，就凭作者的队伍也是罕见的整齐。”事实上，正常的情况应该是，是“韩寒的杂志”就不该“卖成这个样子”。<br />
<br />
　　为什么这才是正常的情况？因为按照常识，一个不读书的青年、一个对历史无知的人、一个缺乏严格思维训练的人，在任何一个正常的国家，想在思想文化界混，是决不可能的，更不能可能成为人们精神上的引导者。但惟独在中国，却恰恰相反：就是这样一个文化无知、言行不一、虚骄幼稚的少年，靠着所谓“独立”、“反叛”、“酷”、“帅”的姿态和在狭隘精神世界里无节制的意淫，居然能如此火爆，那些还在青春期逆反阶段的学生读者就不说了，就连他们这些所谓的文化人，都返老还童、捧之若宝，可见这些人在宣称“回归常识”的同时，几乎无一不是在违反常识！殊不知，韩寒的存在，无论从哪个角度看，都只有反讽的意义，任何一个肯定或溢美韩寒的人，都足以反证他自己是多么没有水准！<br />
<br />
　　各行各业都有自己的行业生态。当广大的市场和媒介被这种无病呻吟、矫揉造作、商业开道的“伪文学”作品充斥，而大行其道、竖为标杆，当幼稚不自知、无知以为荣、自怜而成团成为青年一代的文化心理，我们的文化生态将为之所毁，其流毒程度，恐不下于党八股，最终形成的恶果，必定是――劣币驱逐良币，而对那些真币、那些良币，人们反倒相逢如不相识了。<br />
<br />
　　中国成语有“聚蚊成雷”，但再多的蚊，即便为了共同的“理想”，从两岸三地飞到一起抱成团、组成团、“忽悠”成团，也终究只是蚊声(而且是钻营名利的蚊声)，不是雷声。在一片“众妄所归”的喧嚣里，志士仁人出面点拨真音、驱散蚊虫，无他，不甘坐视无知无识的人乱搞一通而已。<br />
<br />
　　我期待真正的雷声，――如果真是雷的话。(胡胜华)<br />
