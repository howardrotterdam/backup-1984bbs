													<h2>一百年前梁启超改良路线（立宪派）的失败对我们的启迪</h2>
						
						
																											<div id="postmessage_453772" class="t_msgfont">　　　　　一百年前梁启超改良路线（立宪派）的失败对我们的启迪<br />
　　　　　　　　　　　　　　　・王　澄・<br />
<br />
<br />
　　清末，就如何结束两千多年的中国封建皇权统治，有过立宪派和孙中山的革命派的激烈争论。<br />
<br />
　　1898年在北京发生的百日维新（戊戌变法）把康有为和梁启超变成了变法派的思想领袖，后人称他们为保皇派、温和主张、改良派、维新派、变法派。梁启超和康有为不同，梁后来是同意共和的，但是，梁认为从君主立宪走向共和的方式比较温和，可以保持社会稳定、生灵少遭涂炭，而由革命走向共和会给中国带来巨大的动乱。<br />
<br />
　　1903年以前，梁启超曾从事革命运动并失败。自1903年开始他热衷于宣传君主立宪思想。1905年到1907年，梁启超一人主笔的《新民丛报》和革命党的《民报》（胡汉民、汪兆铭、朱执信、汪旭初、刘光汉等执笔，孙中山指导。）就和平改革还是革命掀起了争论。梁启超反对革命的观点是：革命造成流血和破坏都是不对的，“游荡无赖子将利用军兴机会，尽夺富人财富”，而使天下大乱。和平改革也能达到救中国的目的；因为帝国主义正在瓜分中国，革命甚至会导致国家分裂和亡国。<br />
<br />
　　康有为也认为“凡君主专制、立宪、民主三法，必当一一循序行之，若紊其序，则必大乱。”他在《进呈法国革命记序》中，把革命描写得十分残暴吓人；“臣读各国史，至法国革命之际，君民争祸之剧，未尝不掩卷而流涕也。流血遍全国，巴黎百日而伏尸百二十九万变革三次，君主再复，而绵祸八十年。”<br />
<br />
　　而孙中山的革命党认为：中国的政治已经败坏到了极点，“非彻底革新，不足以有为”。<br />
<br />
　　康有为和梁启超一再举例说：英国和日本都是君主立宪体制，不是也进入现代化了吗。<br />
<br />
　　当时梁启超的《新民丛报》和革命党的《民报》发源地在日本，其影响在中国。尽管在争论中革命党的论说很占理，但是，“屁股”决定了一切。当时，全中国同意康梁观点的人占绝大多数，特别是知识分子、官员、乡绅。然而，后来的中国的历史却没有照着康梁立宪派的路子走，而是发生了1911年的辛亥革命，原因在哪里呢？<br />
<br />
　　原因就在于：走不走真正意义上的君主立宪体制之路，不是康梁决定的，是清廷决定的。清廷作为统治集团和利益集团，清朝的皇族和贵族实际上从始到终都不愿意把统治国家的权力交给国会。这才是康梁立宪派的路走不下去的真正原因。立宪在当时是全中国人民的梦想，当这个梦想被清廷破坏后，每一个中国人都转过身来支持革命。<br />
<br />
　　回顾这段历史：<br />
<br />
　　1906年8月25日，在清廷召开的御前会议上，奕匡、徐世昌、张百熙、袁世凯等一派主张：立宪有利无弊，符合民意，应从速宣布。而孙家鼎、荣庆、铁良和瞿鸿S等反对者提出：中国情势与外国不同，实行立宪，必至执政者无权，坏人得栖息其间，为祸非小；人民不知要求立宪，授之以权，不仅不以为幸，反而以分担义务为苦；实行自治，坏人便会掌握地方命脉，会导致底层民怨沸腾，矛头必将指向最高统治层。（摘自【大清帝国最后十年】）（王澄：和今天中共拒绝政治改革的借口完全一样。）<br />
<br />
　　1906年12月16日，预备立宪公会在上海成立。公会宗旨是教育人民，为立宪作好准备。<br />
<br />
　　1908年8月27日，清廷颁布了《钦定宪法大纲》，并明确了九年预备立宪期。《钦定宪法大纲》开宗写道：“大清皇帝统治大清帝国，万世一系，永永尊戴。君上神圣尊严，不可侵犯。”革命党人批判这个大纲为：“彰明昭著地暴露了清政府假立宪之名，行专制之实的用心。”<br />
<br />
　　1908年，光绪皇帝和慈禧太后相继去世，清廷失去了象征性和实质的核心。摄政王载沣采取集权措施，积极推行由皇族独揽国家大权的政策，满洲亲贵和汉族官僚之间的矛盾加深，<br />
<br />
　　1910年，国会请愿同志会在北京连续发起国会请愿运动，要求清政府速开国会。<br />
<br />
　　1911年的“铁路国有”引发了全国的动乱，抗粮抗捐，群众暴动接连发生。<br />
<br />
　　1911年5月8日，清廷成立以庆亲王奕匡为总理的内阁。这个内阁被批评为“皇族内阁”，批评者援引英国皇族不能担任内阁成员的范例。立宪派分享政权的希望完全落空，他们抛弃立宪的幻想，对革命活动开始表示同情。革命的形势愈益成熟。<br />
<br />
　　1911年10月，武昌首义。<br />
<br />
　　1911年11月2日，清廷新的宪法大纲颁布，称《十九信条》。其中：第一条，大清帝国之皇统万世不易；第二条，皇帝神圣不可侵犯；第三条，皇帝之权，以宪法所规定者为限。<br />
<br />
　　今天我们回顾一百年前的历史事件的目的是为今天的中国把脉。从一百年前发生的历史事件中我们可以看出这次中国政治转型应该有以下几个特点（或者叫作共同点）：<br />
<br />
　　1．最高统治层内部意见是不统一的，但是往往保守势力占上风，因为最高统治者害怕自己成为革命的对像。所以，胡锦涛和温家宝本质上都是“维持会”的会长。<br />
<br />
　　2．慈禧太后在世的时候稳住了大局，慈禧太后死后，局面就开始混乱到无法收拾。中国现在没有了像邓小平和慈禧太后这样以个人的威望和权势能够稳定大局的人。<br />
<br />
　　3．大多数国民最初的愿望都是和平转型。<br />
<br />
　　4．但是，全国人民有一个底线，一百年前是真立宪，今天是“零八宪章”、取消一党制。底线也就是共识，过了这个底线人民就会造反。<br />
<br />
　　5．在（武昌首义）革命成功之前，往往有全国性的动乱。一百年前是“铁路国有”引发的大规模动乱。<br />
<br />
　　6．政府腐败像刀子一样每天在割人民身上的肉，腐败是逼人民造反的直接原因。人民对政府一次次的“失望”是向革命转型的加速剂。<br />
<br />
　　7．把人民从和平转型逼向革命的人，不是别人，正是共产党自己。只要共产党否定“零八宪章”，反对民主，坚持专制，中国人民最终就一定要走上革命的道路。<br />
<br />
