																											<div id="postmessage_244928" class="t_msgfont">皮诺切特和小瓶子的区别（节选）<br />
鲁克<br />
<br />
第一， 他们面临事件起因，处理目的不同 <br />
<br />
在1973年，智利左派已经极端疯狂化，微弱多数的阿连德政府，强制推行违反宪法和法律的工农业国有化决议，在没有得到智利的议会接受的情况下，试图进行类似以前的古巴和中国的公有制的强制改造，没收和侵吞大批企业家的私有财产。阿连德是民选总统，但是他的政府的决议，并没有正当性，滥用了人民的赋予政府的权力，他的一系列重大经济和社会改革，实质上是剥夺了人民的私有财产权，这本身就是严重违反宪法！假如没有皮诺切特的政变，智利会马上进行全面的无产阶级革命，那么左派马克思主义者制造的社会大混乱都不可避免，左右派的协商会停滞，冲突会加剧，加上古巴卡斯特罗的挑唆，大规模内战就要爆发，如果阿连德代表的左翼胜利了，可能智利再美洲就是第二个古巴。 <br />
<br />
而在80年代，中国tg高层开始认识执政几十年来的一系列左的政治经济政策的错误，以deng代表的军方开始走务实的道路（所谓“白猫黑猫论”），zhaoziY为首的中G政府在得到务实派的庇护下逐步开始的政治、经济改革，这些改革放松了原来的束缚人民自由的枷锁。农民可以包产到户，个体被允许从事一些商业活动，在市场作交换获得收入，而不再是单单依赖政府的配给和安排，但是由于旧政治体制度和马克思主义教条的约制，政府并没有把一个自由的市场还给人民，也没打算给人民平等的发展机会。而是相反，政党的特权在行政上滥用，特权被寻租获得商业利益，政府的腐败开始加剧。 <br />
<br />
由于终国tg政府不肯彻底放弃旧的计划经济，造成人民经济理念不清。而市场经济机制和计划经济机制碰撞冲突越来越剧烈。物价开始出现大波动，民众生活开始没有安全感，在危难中似乎正失去政府的保障。长期被左派的思想禁锢的民众，根本无法容忍出现中G干部权力寻租--“官倒“的出现，压抑不满的情绪一触即发。 <br />
<br />
向往言论自由的爱国的大学生，挑头开始示威游行，市民随后也走上街头。当时学生思想比较市民更加对向往西方制度，期待政府制度全盘西化。这类宣泄的对政府的不满和愤怒，归结起来都是希望通过和B京（中G中Y）对话协商，让政府还人民一个民主的政治制度和自由的经济制度，公正的司法制度。 <br />
<br />
Deng在这关键时候却左右摇摆不定，害怕顺应“全盘西化”的号召，自己的政党可能像GC主义在东欧一样垮台，而自己不能掌控局势；但是又不愿意向极左派的靠拢，走回头左路，因为他知道那是死路一条（这也就他在92年后，又重提起走开放和发展的道路的原因）。党内的高层纷纷被揭发腐败劣迹，政府威权性随着示威游行的规模扩大渐渐衰弱，军届极左派借机向改革派动手，Deng顺应党内反弹的趋势，决定用兵镇压广场的示威。 <br />
<br />
在1989年时候，Deng当时是十分害怕人民力量的，用坦克和机枪图纱市民和学生的惨剧正表明这点。他说要民主自由也只不过是叶公好龙。他不敢于正视G产主义正在失去民心和政府正在失去人民信任的这一现实。有人说，为了挽救党，deng不得不出如此下下策。 <br />
<br />
智利的皮诺切特代表的是右翼力量，当智利的左翼政府要剥夺人民的私有财产权力时，他用非常手段捍卫了人民的利益，他的部队包围了阿连德总统府，请求阿连德和他的支持者和平离开，结果被拒绝。谈判演变成了武装冲突。 <br />
<br />
假设，皮诺切特不政变。可能被杀害的人也许是他自己，而受牵连，受害人会更加多，狂热的阿连德左翼迟早要清除这些右翼分子。因为这类迫害事件，在前苏联，前东欧，中国大陆，古巴，朝鲜，越南，赤柬，无一例外地都发生过。应该说，幸好阿连德左翼违法宪法的革命被制止了，虽然因此皮诺切特成了历史的恶人。 <br />
<br />
第二， 两人的立场完全相反，对人民有不同的非常手段。 <br />
<br />
皮诺切特是军方强硬右翼。政变后，他不给极端左派的发展的空间，被官方记载文件中，在他执政的17年专制当中，有3197人死于非命，其中2796人死于他和阿连德武装冲突，剩下的才是后来被逐一迫害致死的，据左派的人权组织宣称，他暗地里关押、迫害了3万余人。 <br />
<br />
在关键时候，尤其在决定人的性命安全时刻。独裁者都会扮演强硬角色，dengxiaoP也是一样，不同的是他是左派的代表人物。回顾历史，他领导过的解放战争，还是1957年反右运动，1989年TA门事件，在他手上失去生命的人和逃到海外的简直不计其数，虽然他们被当作阶级敌人，动乱分子。 <br />
<br />
1973年在智利的冲突中，也不少反抗皮诺切特武装的军人和警察被杀害。可是1989年，牺牲的大多是没有武器的市民和青年学生。<br />
皮诺切特后来追捕和迫害的都是社会党人，工会领袖人物，左派学者。在中国1957年被迫害的所谓“右派”几乎都是无辜的人，被逼迫成了政治的牺牲品，<br />
而1989年死去或者逃亡的大部分的学生也谈不上有清晰政治理念，他们既不是左派，也不是右派，只是凭着热血关心国家参与了民主运动，和49年前的左派的地下，地上的供产革命党员很大不同。 <br />
<br />
第三， 两人对国家的遗产不同。 <br />
<br />
皮诺切特政变后，启用 “芝加哥小子”对智利作了彻底的充分的自由经济改革，智利经济得到空前的繁荣，这是古巴等周边国家不可比拟的。他晚年恢复并建立智利的良好的民主机制，也尊重了宪法和法律。他当时明明知道放任自由的全民选举就意味着自己的下台，下台失去权力后意味着自己可能被清算。但是他还是勇敢地做了。 <br />
<br />
可是DengXxiaoP，虽然他用了zhaozY，但是政治体制改革寸步不前，民主选举直到现在也没有实现，他后面的两个领导人都是他指任接班的。 <br />
<br />
经济改革上，进步缓慢，自由市场的基础---保障私有制，这点并没有写在宪法上，相反却被他加上了违宪的四象基吧原则。他设计的不流通的古怪的证券市场，让投资人被圈被骗，他的所谓“摸着归头过河“，走道一半，证明是错的，又重新来过。直到现在资本市场才实现股本的全流通。 <br />
<br />
据华尔街日报，中国国有企业占经济比重最大。政府垄断银行，通讯，能源的主要资源和产业，而这些行业贡献的GDP却是少部分，私有化进程非常缓慢。即使在deng之后的十年，中国经济自由度在世界排名的一百多位，政府的宏观政策随时干涉地产市场和股市市场等投资。 <br />
<br />
