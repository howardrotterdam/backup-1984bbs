还真不能这么说，04年我有几个朋友集资到外地买了套房，（几个人凑尚不够在本地买）一年多后抛出小赚了一笔，结果07年飙升，捶胸顿足 </blockquote></div>记得冯唐在博客中提到过，<br />
我姐过去二十年一直在湾区，她认识一个华裔大姐，生在旧金山唐人街，四十多了，拳脚刀棒不错，几乎一句英文都不会说，几乎没有正式工作做，嫁了个学做芯片设计的清华留学生。2008年初，这个大姐，背着老公，从银行贷款在湾区买了五套房子，全部零首付，全部前三年免息，还送装修。2月份的时候，我姐当笑话讲，我后脖子一凉，距离崩盘不远了。三个月后，经济危机就来了。。<br />
我这里试着分析一下原因：<br />
&nbsp; &nbsp;&nbsp;&nbsp;几个人凑钱买房应该并没有多大问题，即使崩溃也只是损失了他们以前的存款，在危机之后还是容易维持正常的生产生活。但是如果几个人凑钱还不够还要贷款买房，那这个问题就比较严重了，危机之后不光会损失存款还要欠下几年内都不容易还清的外债，那几年之内他们很难正常生活了，得省吃俭用挣钱还债。贷款/借钱与否实际对应着风险的不同。如果只是存款那么对于单个家庭来说风险小得多。如果多数人宁愿冒着巨大的风险来借钱或者贷款买房的话那么危机应该确实不远了。这里的问题是我们个人的感受并不一定反应全社会的经济数据的，周围的人都买了房并不代表全国的人都买了房，这些数据还需要更为详细的调查。有了准确的数据才能有准确的判断。<br />
