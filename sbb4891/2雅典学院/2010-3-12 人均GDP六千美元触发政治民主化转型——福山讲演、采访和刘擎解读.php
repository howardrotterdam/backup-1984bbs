<br />
　　采访部分（节译）<br />
<br />
　　福山：（在谈到权威主义国家的兴起时说）因为这次金融危机，美国的实力大概会相对下降。但是否在权威主义体制下发展的思想就因此占上峰？我看倒也未必。<br />
<br />
　　会田：（谈到中国是否迈向民主主义时问）你认为现代化的过程一直下去最终会走向自由民主主义，这个看法有无改变？<br />
<br />
　　福山：很难讲。最大的问题就是中国。中国在权威主义体制下的现代化取得很大成功。不过中国依然属于较为贫穷的国家。从平均购买力来说（人均GDP）只有四千美元左右，是韩国民主化时的一半，也还不具备类似韩国和台湾（地区）民主化时中产阶级占国民半数以上的那种条件。今后十到十五年，中国保持经济成长的话，是否会形成要求民主化的压力，这是一个很重要的问题。我认为会出现那样的压力，有理由这样认为。中国人的教育水准进一步提高后，市民社会就会一点点成熟起来，人们的意识开始觉醒……这已经开始了，因为人民拥有的信息很多。尽管中国政府管制信息，但人们通过因特网相互联系，交换信息。如此一来，中国社会中就会产生要求政治体制开放的压力。<br />
<br />
　　会田：人民的要求会不会跟体制发生正面冲突？<br />
<br />
　　福山：有可能。中国民主化时，不会模仿美国，或者是依据某些理念进行。大概会因为非常实际的理由而民主化吧。比如，现在地方政府所产生的很多问题，如果不民主化就解决不了。中国几乎每天都有因环境问题而发生的悲剧。还有开发商圈走农民的土地，贪官污吏欺压管辖地区的民众等问题。人们为了解决这些问题只好向共产党的上级机关投诉，而上级机关有时管有时不管。如果政府不进一步实施问责的话，就无法解决这些问题。这就是中国的体制非开放不可的原因。不然的话无法解决地方政府的腐败问题。<br />
<br />
　　除了北京奥运，去年中国还发生了两件大事。一件是四川大地震，还有一件是三聚氰胺中毒事件。通过这两件大事，可以看到中国人意识变化的迹象。在四川大地震中，有志愿者参加救护活动。因学校倒塌而失去孩子的父母们向政府求助，指出是因为地方政府腐败而导致学校建筑变成豆腐渣工程。但是政府不加理会，想用金钱解决……。发生了这样一些情况。<br />
<br />
　　即便如此，还是可以看到公民社会有意识地活动的迹象。三聚氰胺事件也一样。同样是因为地方政府的腐败导致了幼儿们的死亡。人们起来追究贪官们的责任，中央政府也不得不做出回应市民们诉求的姿态。开始出现了一点点类似民主政府问责的现象。不过路途还很遥远，这将是一个极其缓慢的过程吧。<br />
<br />
　　如果没有政府要对人民负责这样一种意识的话，那么在地方上就没有办法实行良好的政治。当公民们能通过司法制度和选举等来惩罚那些腐败官员和犯了严重错误的官员时，才会产生那种责任意识，这就是民主主义的真谛。<br />
<br />
　　现在的中国也有问责这种意识，不过那只是对党中央负责。一旦中央决定了，可以让地方官员听话。但是民主主义需要向下即向公民负责，而这正是中国所完全缺乏的。<br />
<br />
　　会田：那样的权威主义在中国还能维持多久呢?<br />
<br />
　　福山：这很难回答。我觉得至少三十年没有问题吧。不过，经济增长持续下去的话，国民教育程度也会提高，中产阶级也将增加。所有的这一切，都将聚合成走向民主主义的压力。<br />
