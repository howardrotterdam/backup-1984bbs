面对中国模式的历史终结论<br />
<br />
刘擎<br />
<br />
有消息说，弗朗西斯・福山（Francis Fukuyama）最近发生了一次“思想巨变”。据说这位日裔美国政治学家终于被中国的独特发展模式所折服，要重新考虑那个使他声名鹊起的“历史终结论”。消息来源是日本著名政论杂志《中央公论》今年9月号刊登的福山演讲和对他的访谈（题为《日本要直面中国世纪》），经过中文摘译写成报道，在新华网上发布，已被几百个网站转载。这篇报道文章强调，福山特别肯定了“中国模式”的优越性，认为它会受到更多国家的钟情，并指出，“客观事实证明，西方自由民主可能并非人类历史进化的终点。随着中国崛起，所谓‘历史终结论’有待进一步推敲和完善。”<br />
<br />
　　一位竭力宣扬西方自由主义民主最终获胜的明星学者，今天高度肯定中国模式的优越性，这多少让人有些兴奋。但这个好消息似乎来得有些突兀，不免心生疑虑。据我所知，自1989年《国家利益》发表那篇轰动性的文章以来，福山的历史终结论曾遭到各种质疑和批评，曾无数次地被人宣告破产，但他本人一直“顽固地”坚持“自由民主与市场经济最终胜利”的基本论题。迟至2007年10月，福山到布朗大学演讲（当时笔者在那里做访问学者），在演讲后的酒会上，我曾问起他：中国发展的事实是否颠覆他的历史终结论？他未加迟疑地回答说，这大概还要等15到20年才能看得清楚，而目前他还是坚持自己的基本判断。甚至到2008年9月，福山在接受美国《新闻周刊》采访时，被问及“历史的终结背后的论题仍然成立吗”，他的答复是“这个基本假设仍然有效……尽管权威主义在俄国和中国复兴，但自由主义民主仍然是被广泛接受的唯一正当的政体形式”，并表示“就长远而言，我仍然相当确信民主体制是唯一可行的体制”。这篇访谈发表时用的标题是《重归“历史的终结”》（“Back to The End of History”, Newsweek, September 20, 2008）。难道一年之后，他的思想真地发生了“巨变”？<br />
<br />
　　怀着这样的疑问，我向东京大学的友人王前先生求教。王前先生找来当期的《中央公论》，对福山的演讲做了全文翻译、对访谈的重要内容做了摘译（参见附文），还通过电话与我讨论。我们一致的看法是：那篇在网上广泛流传的报道，对福山的某些言论做了脱离语境的“选择性”编译，也因此产生了某种误导倾向。（比如，在那篇报道中有如下“编译”段落：“中国的政治传统和现实模式受到越来越多发展中国家的关注。在印度‘民主’模式与中国‘权威’模式之间，更多国家钟情中国，前者代表分散和拖沓，后者代表集中和高效。”而福山相关的原话是：“看到这些国家和印度那样的民主国家的民主决策过程，很多人赞赏中国的那种权威主义所具备的比较迅速的决策能力。不过，那种权威主义的政体也有其自身的缺陷。因为没有法治，也没有选举进行监督，所以其问责，只是面向上面即共产党和党中央，而非面向政府应该为之服务的市民。”）的确，福山相当重视中国三十年来的发展经验，也发现中国政治文明的独特传统及其对亚洲地区现代化发展的深远影响，甚至认为中国模式的特殊性具有重要的研究价值，成为检测其理论的一个重要案例。但是，他至今仍然没有放弃他的一个核心理念：自由主义民主体制之外的现代化模式迟早会面临民主化的压力，会遭遇巨大的困境而难以长期维系。<br />
<br />
　　也许，福山本人是否改变了自己的观点并不是什么重要的问题。更有意义的问题是：今天我们再来讨论（似乎已经过时了的）历史终结论还有没有必要？或者，在什么意义上这个论题或许仍然值得我们认真对待？历史终结论或许是错的，但它可能并不像听上去那么简单和荒谬。<br />
<br />
　　福山可以算是列奥・施特劳斯的隔代弟子，经历的学术训练也相当奇特。他最初就读康乃尔大学古典学系，师从布鲁姆（Allan Bloom）学习政治哲学，本科毕业后进入耶鲁大学读研究生（主修比较文学专业），曾赴巴黎深造，追随“解构派大师”雅克・德里达和罗兰・巴特攻读“先进的”文学理论，结果读得一头雾水。后来他在回忆这段经历时说，“也许在年轻的时候，你会仅仅因为某种学说很难懂，就以为它必定是深刻的，而你没有那种自信去说‘这不过是胡言乱语’。”福山在巴黎期间还写过小说，但从未发表。六个月之后，对解构派大师完全幻灭的福山回到美国，放弃了比较文学专业，转到哈佛大学在曼斯菲尔德和亨廷顿指导下攻读政治学博士。<br />
<br />
　　就学术风格而言，福山的成名作――1989年的文章《历史的终结》以及1992年的著作《历史的终结与末人》――在当代美国政治学界是相当罕见的：它是政治哲学、思想史、历史哲学与政治科学的某种“奇异”结合。而历史终结论的出笼也明显有施特劳斯弟子们推波助澜的因素。福山的理论最初成型于1988年（早在柏林墙倒塌之前），当时他应邀去芝加哥大学做这一主题的演讲，而邀请者就是施特劳斯的两位大弟子塔克夫（Nathan Tarcov）和布鲁姆。布鲁姆还参加了1989年《国家利益》杂志对福山的回应笔谈，并在临终前为福山1992年的著作写下热情的“封底赞辞”（endorsement）。1999年，《国家利益》杂志组织了对“历史终结论”再思考的讨论，福山发表《重新考虑》（Second Thoughts）一文，表明由于怀疑基因工程技术在“改造人性”方面具有危险的潜力而保留了“历史重新开启”的可能，因此在历史哲学论题上做出了很大让步，但在政治科学的论题上仍然坚持己见。当期杂志还发表了曼斯菲尔德等人“回应福山”的笔谈。<br />
<br />
　　正如塔克夫曾评价的那样，福山作为一个社会科学家和政策研究家，他的知识结构是“相当罕见的结合”。《历史的终结与末人》一书实际上包含着两个互为支持的论证：哲学论证与政治（科）学论证。所谓“历史的终结”并不是指实际“历史事件”的终结，而是在历史哲学（普遍历史）的意义上，主张“历史方向的进程”――意识形态的演进到达了其终点：在自由主义民主之外，没有其它系统的意识形态具有真正的竞争可能。这个哲学论题被福山用来支持（或“包装”）他的政治学论题，这是一种“历史进步观”主导下的现代化理论或民主化理论：西方现代的政治经济制度（与现代科学技术相似），虽然发源于西方但并不是地域性的，而是现代化普遍进程的方向。<br />
<br />
　　但是，福山的历史哲学论题能成立吗？他反复声称自己在历史观上是“一个马克思主义者”，但他用（他所理解的）马克思主义史观来否定马克思主义的结论没有自相矛盾吗？他用黑格尔的德国哲学传统来支持英美式的自由主义是否混淆了“自由”的不同观念？马克思所言的“物质性需求”与黑格尔理解的“承认的欲望”是可以如此方便地嫁接起来、成为他哲学论证的两个支柱吗？他对尼采和科耶夫的理解与运用是准确和正当的吗？所有诸如此类的问题都具有争议，也并不容易澄清（德里达在《马克思的幽灵》中对福山那种迂回纠结的“福音书”批判就是一例）。无论如何，福山的旁征博引以及魔术师般的理论创新能力与他对经典思想家们的可疑援用（或挪用）都同样令人惊讶。只是研究西方思想经典的学者可能会好奇：以“严谨解读经典”著称的施特劳斯派的弟子们为什么会热衷于福山的历史终结论？这里会有什么难以参透的“微言大义”？这可能需要另一篇文章来处理，也并不是本文关切的要点所在。<br />
<br />
　　实际上，福山后来的一系列研究越来越远离政治哲学的论述，越来越接近典型的美国政治科学的规范。换言之，他更为靠近他的另一个导师――亨廷顿，虽然他们之间存在着重要的理论竞争。于是，福山晚近对历史终结论的表述已经脱去了华丽的哲学外衣，基本上还原为（半个世纪前兴起的）现代化理论的一个“升级版”（例如，2007年6月他应Long Now Foundation之邀在旧金山所做的演讲“The End of History Revisited”）。他的理论逻辑也因此变得更为明晰，可以（难免有简单化地）概括为三阶段的现代化普遍进程：（1）任何现代化发展都必然需要现代科学技术，这是普遍化进程的基本动力和共同平台；（2）科学技术的效益最大化模式将导致经济结构的普遍化，自由市场经济是效益最优化的经济制度；（3）市场经济的发展和人均收入的提高会导致一系列社会结构和文化价值的变化（包括城市化、产业工人阶级的扩大、教育的普及提高、信息传播渠道的丰富多样、专业阶层的兴起、财产的积累和产权意识增强、市民社会的丰富发展等等），这将逐步改变传统的政治文化，产生越来越强烈的公民政治参与要求，也就是民主化的压力。当这种压力超过一定“阀值”，会最终导致政治制度的普遍化――走向自由主义民主体制。<br />
<br />
　　福山还指出，在这三个阶段的普遍化进程中，现代化与现代科技的关联是必然的；现代科技与市场经济的关联不是决定论式的，但仍然相当紧密；而从市场经济到自由民主的递进关系更为松散也最为复杂（包括本土文化的复杂作用），但还是有很强的内在关联――由于自由民主政体最有能力回应现代化进程中产生的自由与平等的诉求，因此是最具有正当性的政体。福山与亨廷顿的一个分歧在于：亨廷顿倾向于认为民主政治是基督教文化的特定产物（所谓“文明冲突论”的主题之一），而福山则认为现代化本身就具有一种文化塑造力量，最终会在各种文化的特殊性中生成一种共同的政治文化维度。<br />
<br />
　　福山的这个论证能成立吗？在这里，我们回到了西方政治学界的一个经典问题：现代化与民主的关系。从1959年李普塞（Seymour Lipset）提出著名的“社会条件论”到最近十多年来的大量研究，都表明经济发展水平与民主进程之间存在着重要的关联，但同时也发现两者之间的关联是多样和复杂的，对此做出了多种竞争性的理论解释。然而，无论在理论上如何解释，多数政治学家都承认一个重要的经验事实：当一个国家的人均GDP到达一定水平之后，非民主的政体就很难维系，而且民主化转型一旦发生也很难逆转。但这个“一定水平”的指标是多少？最早说是4000美元，后来又说是6000美元（以1992年的美元购买力平价为基准）。对于这个“转型阀值”的确切数值当然还有争议，但可以确认的事实是：目前在全球范围内，人均GDP超过1万美元的国家（除去少数石油输出国之外）全部都是所谓“自由民主政体”。<br />
<br />
　　实际上，福山的学术底色是“社会科学家”，他对西方民主必胜的信念很大程度上依赖于经验证据。在大约三十年前，学术界曾盛行过所谓“东亚例外论”――权威主义政体下的高速现代化发展似乎可以脱离民主化的普遍进程。但后来当韩国和台湾地区的人均GDP达到了6000美元左右，就发生了民主化转型。从福山1997年发表的文章《例外论的幻觉》（The Illusion of Exceptionalism）中可以看得出，东亚的转型对他有深刻的影响。而1989年东欧阵营的政治巨变以及1991年苏联的解体，更强化了他“政治发展殊途同归”的信念。后来的“9・11”事件以及南美的查韦斯“红色风暴”，都没有改变他的信念。福山多次指出，民主化进程可能会出现回潮和逆转，也承认这个进程比他最初预想的要更漫长更艰难（近年来他也强调，在没有基本健全的法治条件下发生民主化转型将是危险的），但他仍然相信，“就长远而言”民主政体仍然会在曲折发展中越来越盛行。实际上，他并没有排斥而是吸纳了亨廷顿早年的一个思想：“政体能力”问题（国家维护政治秩序和治理的能力）有时比“政体类型”问题（民主还是非民主）更为重要。近年来福山对民主政体的辩护更侧重于在政体能力方面做出论证。他指出，权威国家并不一定具有更强的治理能力。在论及中国当前出现的腐败与环境污染等问题时，他认为这主要不是由于中央政府的疏忽或缺乏解决问题的意愿，而是其治理能力不足造成的（“China’s Powerful Weakness”, Los Angeles Times, April 29, 2008）。在他看来，政体能力与政体类型最终将综合为一个论题：当现代化发展达到一定水平之后，民主制度――不仅会在政治正当性方面，而且也会在政体能力方面――明显优于权威政体，所以才更有吸引力，才是理性的制度选择。在这个意义上，“就长远而言”自由民主政体具有难以抗拒的优势。<br />
<br />
　　但是，福山反复指称的“长远”到底有多远？无限期的“长远”会使他的理论永远免于经验“证伪”而沦为（卡尔・波普尔界定的）“伪科学”。福山当然明白这个基本的社会科学道理，所以，他多次提及人均GDP达到6000美元（1992年的美元购买力平价）这个“转型阀值”。我们有理由相信：这是检验福山理论之有效性最为明确、最具经验可测性的标准。二十年来，福山之所以仍然坚持己见，是因为他的理论还未真正遭遇有意义的“证伪”案例，但他终究要面对一个史无前例的检测――来自中国经验的检测。中国三十年来的迅猛发展具有非同寻常的意义，也必将成为历史终结论的一个判决性案例：“中国模式”究竟只是一个过渡性的阶段发展模式（一个巨型的“东亚模式”），还是一个最终打破了普遍现代化理论的独特“文明模式”？对此，福山似乎有些犹疑不决。2008年中国的人均GDP已经超过了3000美元（PPP调整后的指标在4000美元左右），而且目前仍然保持着10%左右的年增长率。那么，福山本人完全有可能在其有生之年见证这个检测的结果。<br />
<br />
　　如果说“面对中国模式的历史终结论”是福山的问题，那么我们的问题是“面对历史终结论的中国模式”。如果中国在经济高速发展的同时，能够更加完善自己独特的（不同于西方竞争选举、议会与多党政治以及分权制衡的）社会主义民主，能够建立一个更加繁荣稳定、共同富裕、自由和谐、平等公正的社会，能够发展既承传中国传统文化又汲取世界文化精华的精神文明，那么，中国模式就不是一个过渡期的“转型模式”，而是一个生生不息的“文明模式”。（到那时候，历史终结论自然会进入历史博物馆，而福山及其信徒或许也会心悦诚服地向中国致敬。毕竟，在福山的笔下，“末人时代”的来临也不是多么值得庆贺的前景。）但在此之前，历史还在展开，竞争仍在继续。对于西方自由民主的信奉者来说，历史终结论是一个信念，也是一个赌注；而对于中国独特道路的捍卫者来说，它不只是一个论敌，也是一桩尚未了却的心事。<br />
