													<h2>人均GDP六千美元触发政治民主化转型――福山讲演、采访和刘擎解读</h2>
						
						
																											<div id="postmessage_513902" class="t_msgfont">日本要直面中国世纪<br />
<br />
　　――弗朗西斯・福山接受关西大学名誉博士学位时的演讲<br />
<br />
　　（原文刊登于《中央公论》2009年9月号）<br />
<br />
　　会田弘继 编辑采访<br />
<br />
　　王前 译<br />
<br />
　　会田弘继按：福山正在写作有关形成国家的纵轴（历史）和横轴（现在）的大作（预计两卷本）。在该书中，福山将把我们所目睹的“中国的兴起”放在跟西欧现代政治思想不同的脉络中来展望人类社会的未来。这部著作必将像他之前的著作《历史的终结》一样引起强烈反响。在采访部分请福山围绕中国和美国、日本以及资本主义的现状谈了他的看法。希望有助于大家了解福山这位思想家是如何看当今世界的。福山是现在已经逐渐被忘却的战前日本的大学者、原京都大学教授河田嗣郎的外孙（译者按：河田与日本马克思主义经济学家、思想家河上肇是好友）。他的孙子即现任关西大学校长河田悌一（译者按：专攻中国思想史，留美时曾接受过余英时和史景迁的指导）。<br />
<br />
　　演讲部分（全文）<br />
<br />
　　今天我想讲讲亚洲的国家制度的传统。<br />
<br />
　　最近大家经常议论中国在国际政治里的定位问题。因为中国不仅是世界经济中举足轻重的国家，而且在安保方面的作用也在扩大。这些都是重要问题。不过，今天我要着重讲的是别的问题，即中国的国家制度的传统。这也是我正在写的关于“政治发展”一书的主题。我正在写的书要讨论三个重要的政治制度。一，国家；二，法治；三，对市民负责的统治制度（即民主主义）。<br />
<br />
　　也许从传统的“现代化理论”来看是难以想象的，我考察“政治发展”的历史不是从欧洲开始，而是尝试从亚洲开始。不再追问为何世界各地出现了跟欧洲不同的各种各样的社会，而是把中国作为“政治发展”的一个典型看待，去探求为何其它社会跟中国模式分道扬镳。<br />
<br />
　　把中国作为典型，并非任意的选择。中国是世界上最早产生了现代国家的文明体。我这里说的“现代国家”是按照马克斯・韦伯的定义，也就是说在某个划定的区域里垄断合法暴力的行使，依据机械的选择标准、按能力选拔并雇用专家，长期维持由这些专家组成的官僚机构，其权限渗透到所支配社会的每个角落，拥有能改变该社会形态的权力。这就是国家。<br />
<br />
　　在此意义上的最早的国家，约八千年前的新石器革命后，出现在美索不达米亚平原、埃及和墨西哥盆地等地。中国则是经历了从春秋时代到战国时代（东周王朝，公元前770年至公元前221年）五百年左右的激烈的战争时期，产生了许多城邦国家群，它们合并而成若干具备了政治机构的较大的领土国家。<br />
<br />
　　为了进行战争，急需建立能充分动员的体制，其结果便是发展了官僚行政制度，渐渐地放弃原先的那种中国历史上典型的世袭家臣制度，代之以由根据能力选拔出来的官僚主导的行政。这个过程最早的结果就是秦朝（中国最早的中央集权国家）。秦在公元前221年统一中国，建立了第一个中华帝国。<br />
<br />
　　对古代中国所创建的现代国家，欧美学者注意得不够充分。即便是对中国很了解的韦伯也只不过一直把它看作是家产国家的行政，是一种历史悠久的权威主义。但是中国的政治制度比那些制度要先进得多，在国家行政的层次上遥遥领先于同时期的罗马帝国。欧洲发展出一套跟中国相匹敌的国家制度要晚十八个世纪。<br />
<br />
　　的确，中国很早就建立了现代国家制度。不过缺乏政治发展所需要的其它一些重要侧面，就是法治和对市民负责的统治制度。<br />
<br />
　　所谓“法治”，是指位于政治主权者之上的法律体系，它对主权者的决定进行制约。古代以色列和希腊、罗马和印度都有法治的概念，但是中国一直没有。也许原因之一是中国的法律不是建立在宗教基础上的。在中国皇帝说是法就都是法，所以没有可以推翻皇帝圣旨的制度。<br />
<br />
　　现代所说的民主主义，即对市民负责的统治制度在世界各国发展起来，从时间上看，比国家制度和法治出现得要晚得多。<br />
<br />
　　对市民负责并非一定是普选制度的产物。统治者即便没有对其权力进行限制的制度，也可以培养出对其统治对象即人民承担道德责任的意识。中国也产生了这种意识，在很多意义上它成为儒家思想的核心。但是将这种责任制度化，由普通市民和从市民中选出来的人对统治者进行监督，甚至在需要的时候将其轮替掉，这样的制度在中国没产生过。<br />
<br />
　　之所以如此，根据我对历史的思考，是因为中国历史上没有出现过国家之外的制度化了的强有力的组织。比如庄园贵族，独立的商业都市，组织起来的农民阶级等。如果有这些的话，就能成为对抗国家的力量。<br />
<br />
　　讲到绵延至今的中国的制度传统，那就是强有力的中央集权的国家机构和比较优质的统治制度。太平的时候，中国的国家所显示的那种要为国民尽最大责任的道义义务感是在其它国家所看不到的。<br />
<br />
　　在权威主义体制下推进现代化并且成功的例子，事实上都集中在东亚。韩国、台湾（地区）、新加坡、越南，还有中国（大陆）。都在历史上受过中华文明的影响。这些国家所实现的经济与社会的发展，跟中南美和非洲及中东处于类似环境下的政府相比，可以说前者做得非常出色。<br />
<br />
　　但是中国的传统里没有法治，也没有民主主义，所以一直没有任何能防止中国历代政权陷入专制的制度。这在毛泽东时代也可以看到。<br />
<br />
　　日本、韩国、台湾等东亚国家（和地区）除了有给它们带来了不起的成功的各种国家机构之外，还不断努力建立法治和民主主义。在这个过程中有时也会碰到严重挫折。每个国家都经历了政治的停顿、领导者的无能和无法解决重要社会问题等情况。<br />
<br />
　　看到这些国家和印度那样的民主国家的民主决策过程，很多人赞赏中国的那种权威主义所具备的比较迅速的决策能力。不过，那种权威主义的政体也有其自身的缺陷。因为没有法治，也没有选举进行监督，所以其问责，只是面向上面即共产党和中央政府，而非面向政府应该为之服务的人民。<br />
<br />
　　现代中国存在许多问题，诸如贪污、环境破坏、财产权等等，现行的政治制度无法妥善解决。中国经常发生的对健康、环境问题的遮掩，若跟民主国家日本是如何处理水俣病的做一个对比，就能明白问题所在。<br />
<br />
　　中国给整个东亚创造了强大而有能力的现代性统治机构，这是支撑了第二次世界大战后的东亚经济奇迹的宝贵传统。不过真正的现代政治制度除了强大的有能力的国家机构外，还需要同时具备“法治”和“问责”。政治的发展并非停止在公元前221年。要建立起同时具备“法治”和“问责”的现代政治秩序有时会经历挫折，是很费时间的过程。不过为了建立起好的统治制度，需要放长眼光，这样的过程是必须的。<br />
<br />
