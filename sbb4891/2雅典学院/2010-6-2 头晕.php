																											<div id="postmessage_654747" class="t_msgfont">　　　　　　　　　　　　中共内报“六四”伤亡密情<br />
　　　　　　　　　　　　　Sat，06，Nov，1999<br />
　　　　　　　　　　　　　　争鸣记者　　罗冰<br />
<br />
　　关于一九八九年“六四”屠城的伤亡人数，中共官方始终没有公布过。事隔七年后，江泽民支支吾吾地说出了一个数字。三月初，在中国社会科学院的《当前社会状况》研讨会上，有较详细、具体的披露，但都是内部档案材料，不公开，不传达。<br />
<br />
　　现实中，有些谜是很难猜破的。一九八九年“六四”的伤亡人数就是这样的谜。当时代表中共官方的张工、袁木之流说没死一个人（有时说是天安门广场），那是信口雌黄，一派胡言，骗人的鬼话。“六四”已经过去七年了，没有知情权的中国人还是无法知道确切的数字。这页历史将来怎样写呢？总不能写“没死一个人”吧！<br />
<br />
　　今年“两会”期间，有些民主党派的人大代实在憋不住了，在江泽民出席民主党派分组会议时，便面对面地向江泽民提出了这个问题：“一九八九年的‘六四’，伤亡人数到底是多少？”<br />
<br />
　　　　　江泽民今年“两会”期间谈“六四”<br />
<br />
　　根据中国农工党中央、中国致公党中央内部传达，江泽民当时的回答如下。江泽民说：<br />
<br />
　　“当时的情况确实很乱。在这个由动乱演变为暴乱的过程中，两种不同性质的矛盾交错著。中央是没有估计到会演变成这么大规模的动乱，又持续了四十多天。这期间，赵紫阳又搞了机会主义……好在由于小平同志的洞察和决断，并得到其他老一辈同志和政治局大多数同志态度鲜明的支持。<br />
<br />
　　“当时，我是参加了五月三十日的政治局会议的，对情况是清楚的。小平同志说，情况已经摆明了，已经从动乱演变为要推翻中央政府，反对共产党领导的暴乱，不能再退了，不能任其恶化下去了。不平息，可能会难以收场。当时，小平同志提出：中央要统一，要把情况向各省通报，同时采取措施。<br />
<br />
　　“关于对八九年春夏之交这场有外国势力支持、鼓动的反革命动乱事件，中央是作了反思和总结的。从党内自身问题、党的工作、教训等都作了多次总结。八九年这场反革命动乱，中央是付出不小的代价。<br />
<br />
　　“关于死亡人数，有五百余人，主要是外地职工、学生和待业人员。当时初步统计死亡人数是二百余人。经过覆核，大多是送医院后救治无效死亡的；有一万余人受伤。戒严部队、武警、公安人员有多人牺牲、六千多人受伤。”<br />
<br />
　　　　　《当前社会状况》研讨会<br />
<br />
　　今年三月初，中国社会科学院在《当前社会状况》研讨会上，首次公开了八九年春夏之交北京和各地政治风波事件中，学生、市民、军队、公安、武警的伤亡人数。这是经中共中央书记处批准，在特许的会议上公开的，但以不传达、不扩散为原则。下面是摘自该会上公开资料的抄件。<br />
<br />
八九年春夏三百多万人次参加示威<br />
<br />
　　一九八九年四月、五月、六月初，全国有二十一个城市发生五千人以上规模的游行、示威、集会、请愿活动，参加的共有三百七十二万人次。其中有大专院校学生和教员、工人、农民、党政机关干部、知识分子、民主党派工作人员、复员军人、中小学学生和教员、退休职工等。<br />
<br />
廿一座城市发生五千人以上的示威游行<br />
<br />
　　这二十一座城市是：北京、天津、上海、广州、武汉、成都、贵阳、哈尔滨、渖阳、兰州、太原、重庆、长沙、乌鲁木齐、南京、郑州、南宁、西安、南昌、杭州、石家庄等。<br />
<br />
　　除北京外，上海、广州、武汉、成都、贵阳、郑州、渖阳、哈尔滨、西安、兰州等地都发生了暴力冲击执法机关、破坏交通、焚烧国家建筑物、残害无辜等严重事件。<br />
<br />
公安部关于各地伤亡统计资料<br />
<br />
　　中共公安部一九九○年七月十日第五次呈国务院报告《有关各地动乱、暴乱中伤亡情况统计资料汇总》：<br />
<br />
　　北京市<br />
　　　　群众死亡：五百二十三人<br />
　　　　群众受伤：一万一千五百七十余人<br />
　　　　军警死亡：四十五人<br />
　　　　军警受伤：六千二百四十余人<br />
<br />
　　成都市<br />
　　　　群众死亡：二百七十七人<br />
　　　　群众受伤：二千一百余人<br />
　　　　军警死亡：九人<br />
　　　　军警受伤：五百五十余人<br />
<br />
　　武汉市<br />
　　　　群众死亡：十二人<br />
　　　　群众受伤：一百七十余人<br />
　　　　军警受伤：一百二十五人<br />
<br />
　　贵阳市<br />
　　　　群众死亡：二十九人<br />
　　　　群众受伤：二百九十余人<br />
　　　　军警受伤：一百五十余人<br />
<br />
　　哈尔滨市<br />
　　　　群众死亡：七人<br />
　　　　群众受伤：九十余人<br />
　　　　军警受伤：一百九十余人<br />
<br />
　　郑州市<br />
　　　　群众死亡：六人<br />
　　　　群众受伤：一百三十余人<br />
　　　　军警受伤：一百五十余人<br />
<br />
　　兰州市<br />
　　　　群众死亡：二十一人<br />
　　　　群众受伤：二百余人<br />
　　　　军警死亡：二人<br />
　　　　军警受伤：一百二十余人<br />
<br />
全国伤亡人数及北京市死亡情况资料<br />
<br />
　　一九八九年四月、五月、六月初，全国发生政治动乱、暴乱期间，共有九百三十一人死亡，二万二千余人受伤。<br />
<br />
　　北京市在动乱、暴乱中死亡情况：<br />
<br />
　　人数：五百二十三人。其中：<br />
<br />
北京学生&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; 五十七人<br />
北京市居民&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; 四十五人<br />
外地学生&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; 一百七十一人<br />
外地职工、居民、农民&nbsp; &nbsp; &nbsp; &nbsp; 二百二十九人<br />
身份不详&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; 二十一人<br />
<br />
北京市府戒严部队关于伤亡人数统计<br />
<br />
　　北京市政府、戒严部队指挥部关于伤亡人数统计报告资料挡案：<br />
<br />
一九八九年六月四日下午六时报告：&nbsp; &nbsp; &nbsp; &nbsp; 现场死亡及送抵医院证实死亡人数，一百四十九人。<br />
　　　　　六月八日第二次报告：&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; 死亡人数，二百六十一人。<br />
　　　　　六月十八日第三次报告：&nbsp; &nbsp; &nbsp; &nbsp; 死亡人数，四百一十九人。<br />
　　　　　八月十五日第五次报告：&nbsp; &nbsp; &nbsp; &nbsp; 死亡人数，四百一十九人。<br />
　　　　　十月十五日第五次报告：&nbsp; &nbsp; &nbsp; &nbsp; 死亡人数，四百八十五人。<br />
　　　　十一月二十二日第六次报告：&nbsp; &nbsp; &nbsp; &nbsp; 死亡人数，四百八十五人。<br />
一九九○年一月五日第七次报告：&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; 死亡人数，四百九十人。<br />
一九九○年四月十日第八次报告：&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; 死亡人数，五百一十五人。<br />
<br />
　　　　　历史之谜<br />
<br />
　　从以上中共官方所披露的数字看，并不能完全令人信服；但比起当初袁木、张工之流的一派胡言，总算是有了一个正式的官方统计数字。真实的死亡数字究竟是多少，恐怕己成了历史之谜。只有等“六四”平反的那一天，也许才能大白于天下。<br />
<br />
　　人们相信这一天一定会到来的。<br />
<br />
摘自【争鸣】月刊１９９６年六月号<br />
<br />
　　海生曰：世上的当政者们一直有说谎到底的坏习惯，而极权政治的中共更是此中的高手。上面这些数字，如果不是当政者有意要放出来试探空气，则世人连这种“精心制造”出来的数字都无法得见。如果这些数字是真的，尤其是北京的死亡数字是真实的话；一个很简单的问题是：为什么当政者不敢向中国人民公布真相？<br />
<br />
　　记得当年的国民党一直“坚持”：谓二二八大屠杀的受难者人数只有“数百人”，而很多世人（包括我在内）在很多年内也一直以为受害者人数在二千左右，残酷的事实是，在二二八真相揭晓后，这受难者人数竟在二万以上！<br />
<br />
　　共产党能够战胜当年的国民党，除了当年的国民党自己腐败不堪外，共产党的手段比国民党更毒辣是一个主要原因。相信这“六四”的真相也可能比二二八更可怖。<br />
<br />
海生怒涛　于枫叶之国　屠龙之都</div>

							
							
							
							
