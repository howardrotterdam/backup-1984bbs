<br />
2010年02月26日 凤凰网历史<br />
<br />
核心提示：伊藤博文从英国留学归来，大倡优化种族之论，认为黄种人荏弱不堪，不及白种人远甚。他当政后，即实施“谋种”政策，凡欧美白人入其境内，辄鼓励日本女子与其野合，以改良种族。谭嗣同对此十分羡慕，然亦知此法断难行于中国，于是喟然作诗曰：“娟娟香影梦灵修，此亦胜兵敌忾俦。蓦地思量十年事，何曾谋种到欧洲？”<br />
<br />
（凤凰网历史频道专栏作者夏双刃供稿）<br />
<br />
宋儒陆象山云：“东海有圣人出焉，此心同，此理同。西海有圣人出焉，此心同，此理同。”<br />
<br />
清朝征剿太平天国时，开始参用西法训练士兵，并聘用了戈登、华尔、买忒勒等洋将。戈登的洋枪队对太平军造成了很大的麻烦，战事既了，他又远赴埃及投机，结果被埃及民族军队击毙。华尔在中国阵亡时，遗命穿清朝官服入殓。买忒勒则颇知中国古书，曾有某清朝官员冒着大雪前来拜访他，他一面用西方礼节与来客握手，一面吟诵《诗经》的句子：“北风其凉，雨雪其。惠而好我，携手同行。”后来他在攻打绍兴时阵亡。<br />
<br />
1885年，福泽谕吉在《脱亚论》中指出：西洋文明之风东渐，所到之处，草木无不披靡。日本国朝野无别，万事采用西洋近时文明，主义所在，唯‘脱亚’二字。日本国土虽在亚细亚东边，其国民精神已脱出亚细亚固陋，移向西洋文明。但不幸近邻有国，一曰支那，一曰朝鲜，不知改进之道，恋恋于古风旧惯之情无异乎百千年之古昔。论教育则儒教主义，虚饰外表，实不知真理原则。道德扫地，残酷无廉耻之极，犹傲然不念自省。自今不出数年其国必亡，国土由世界文明诸国分割。我国不可迟疑，待邻国开明而共兴亚细亚，宁脱其伍，与西洋文明国共进退。对待支那、朝鲜之方法，亦不必因邻国而特予理会，正可从西洋人之风处理。<br />
<br />
魏源早就指出：“欲悉各国情形，必先谙其言语文字……而中国迄今无熟悉外国语言文字之人，恐无以悉其底蕴。”20多年后，迫于当时形势，清廷才设立了中国近代第一所外文学校――“同文馆”。然而在日本，魏源的《海国图志》早就成为知识界人手一册的经典。<br />
<br />
甲午海战中，丁汝昌败退至威海卫，接到了日本海军总司令伊东祜亨下“劝降书”，其文足作警世之用。书全文曰：“时局之变，仆与阁下从事于疆场，抑何其不幸之甚耶？然今日之事，国事也，非私仇也，则仆与阁下友谊之温，今犹如昨。仆之此书，岂徒为劝降清国提督而作者哉？大凡天下事，当局者迷，旁观者审。今有人焉，于其进退之间，虽有国计身家两全之策，而为目前公私诸务所蔽，惑于所见，则友人安得不以忠言直告，以发其三思乎？仆之渎告阁下者，亦惟出于友谊，一片至诚，冀阁下三思。清国海陆二军，连战连北之因，苟使虚心平气以查之，不难立睹其致败之由，以阁下之英明，固已知之审矣。至清国而有今日之败者，固非君相一己之罪，盖其墨守常经，不通变之所由致也。夫取士必以考试，考试必由文艺，于是乎执政之大臣，当道之达宪，必由文艺以相升擢。文艺乃为显荣之梯阶耳，岂足济夫实效？当今之时，犹如古昔，虽亦非不美，然使清国果能独立孤往，无复能行于今日乎？前三十载，我日本之国事，遭若何等之辛酸，厥能免于垂危者，度阁下之所深悉也。当此之时，我国实以急去旧治，因时制宜，更张新政，以为国可存立之一大要图。今贵国亦不可不以去旧谋新为当务之急，亟从更张，苟其遵之，则国可相安；不然，岂能免于败亡之数乎？与我日本相战，其必至于败之局，殆不待龟卜而已定之久矣。既际此国运穷迫之时，臣子之为家邦致诚者，岂可徒向滔滔颓波委以一身，而即足云报国也耶？以上下数千年，纵横几万里，史册疆域，炳然庞然，宇内最旧之国，使其中兴隆治，皇图永安，抑亦何难？夫大厦之将倾，固非一木所能支。苟见势不可为，时不云利，即以全军船舰权降与敌，而以国家兴废之端观之，诚以些些小节，何足挂怀？仆于是乎指誓天日，敢请阁下暂游日本。切原阁下蓄余力，以待他日贵国中兴之候，宣劳政绩，以报国恩。阁下幸垂听纳焉。贵国史册所载，雪会稽之耻以成大志之例甚多，固不待言。法国前总统末古末哑恒曾降敌国，以待时机；厥后归助本国政府，更革前政，而法国未尝加以丑辱，且仍推为总统。土耳其之哑司末恒拔香，夫加那利一败，城陷而身为囚虏。一朝归国，即跻大司马之高位，以成改革军制之伟勋，迄未闻有挠其大谋者也。阁下苟来日本，仆能保我天皇陛下大度优容。盖我陛下于其臣民之谋逆者，岂仅赦免其罪而已哉？如\本海军中将，大鸟枢密顾问等，量其才艺，授职封官，类例殊众。今者，非其本国之臣民，而显有威名赫赫之人，其优待之隆，自必更胜数倍耳。第今日阁下之所宜决者，厥有二端：任夫贵国依然不悟，墨守常经，以跻于至否之极，而同归于尽乎？亦或蓄留余力，以为他日之计乎？从来贵国军人与敌军往返书翰，大都以壮语豪言，互相酬答，或炫其强或蔽其弱，以为能事。仆之斯书，洵发于友谊之至诚，决非草草，请阁下垂察焉。倘幸容纳鄙衷，则待复书赉临。于实行方法，再为详陈。”<br />
