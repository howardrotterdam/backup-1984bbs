康有为赴北京参加顺天府乡试，途径香港、上海、天津等地，游历租界，见西人宫室之瑰丽、道路之整洁、巡捕之威严，乃知西人治国有法度，不得以古旧之夷狄视之。于是大购汉译西学之书，潜心攻读。<br />
<br />
伊藤博文从英国留学归来，大倡优化种族之论，认为黄种人荏弱不堪，不及白种人远甚。他当政后，即实施“谋种”政策，凡欧美白人入其境内，辄鼓励日本女子与其野合，以改良种族。谭嗣同对此十分羡慕，然亦知此法断难行于中国，于是喟然作诗曰：“娟娟香影梦灵修，此亦胜兵敌忾俦。蓦地思量十年事，何曾谋种到欧洲？”<br />
<br />
戊戌变法期间，陈鼎的改革建议较之谭嗣同等更为激进。他认为赶超英美，有四个当务之急：一是变服装，即穿西服，如此一来就不会再把奇装异服的西方人当成“鬼”了，有利于学习西方；二是合宗教，即合并基督教和孔教，彼此都要顶礼膜拜；三是通语言，要求士人尽学西语；四是通婚姻，鼓励中国人与西方人通婚，如此才更可广泛地认知西方。陈的建议引起了光绪的兴趣，曾令军机大臣和总理衙门大臣认真阅读。<br />
<br />
李鸿章访问美国，有记者问他是否可以将美国的报纸介绍到中国，他答道：“我国办有报纸，但遗憾的是我国的编辑们不愿将真相告诉读者，他们不象你们的报纸敢讲真话，只讲真话。我国的编辑们在讲真话时十分吝啬，他们只讲部分的真实，而且他们也没有你们报纸这么大的发行量。由于不能诚实地说明真相，我们的报纸就失去了新闻本身的高贵价值，也就未能成为广泛传播文明的方式了。”<br />
<br />
德国皇帝威廉二世对袁克定说：“中国的东邻日本，奉天皇为神权；西面的英、俄，也以帝国为根本制度。中国地广人众，位于日、英、俄之间，离合众共和的美国则很远。美国人断然不能远渡重洋，来作中国的强援。如今中国的共和制度刚刚建立，执政者多为帝国旧臣，而革命分子势力脆弱。以袁大总统的威望，一变民国为帝国，变总统为皇帝，这正是英、日、俄各国的愿望。德国誓以全力襄助此事！”袁克定大喜过望，回国后即转述与袁世凯，极力怂恿称帝之事。<br />
<br />
民国初年，正值德国威震欧洲，北洋军遂处处效仿德军。不仅教官选择、阅兵步伐、制服款式等均向德国看齐，连蓄须也流行德皇威廉二世的八字牛角须，袁世凯本人如此，其部将曹锟、冯国璋、段祺瑞……一概如此。<br />
<br />
章太炎曾撰文痛贬西方人所用之铅笔、钢笔，并考证出中国古代已知使用铅笔，只是后来才衍化为毛笔。他说：“展转蜕变，毫之制造愈良而铅铁遂废不用，欧洲则讫今未改，以笔言之，亦见汉土所用为已进化，而欧洲所用为未进化者也。”<br />
<br />
顾维钧任总统府秘书时，将他在哥伦比亚的老师古德诺推荐给袁世凯作法律顾问。古德诺当时是哥伦比亚大学的法学院院长，也是全球政治学和行政学的权威。他忠人之事，结合中国的历史传统和实际情况，写出了著名的《共和与君主论》，主张中国实行君主制度。他说：“以中国的历史文化、社会经济和教育水平情况来看，既不能一成不变，又不宜变得面目全非，相对于民主共和政体，显然以君主立宪为佳。”他本是提交给袁世凯作参考的，不料却被袁世凯遍示于众。古德诺因此成为跳梁小丑，并失去了代表共和党竞选下任美国总统的机会。<br />
<br />
蒋介石留学日本时，常在电车、火车或渡轮上看到日本人阅读王阳明的《传习录》，许多人读一章节，即闭目静坐，若有所思。蒋大受刺激，遂买了大量王阳明的著作开始研读。他说：“对于这个哲学，真是一种手之舞之，足之蹈之，一种心领神驰的仰慕……”<br />
<br />
罗素说：“我爱中国人，但是显而易见的，为了抵抗凶恶的军国主义，中国的文明将大部分被摧毁，他们似乎没有别的路可走，不是被征服，就是采用他们敌人的各种劣习。”“新中国应该为自己设立目标。要保存中国固有的民族性：文雅和礼节，坦诚和谦和，加上有西方的科学知识，且把它应用在中国的实际问题上。”他相信，除非中国保存其固有的爱好和平的生活哲学，不学西方对人对事的侵略态度，否则中国采取了西方的政府制度和工业化，不一定能增进世界和平的机会。<br />
<br />
鲁迅说：“与其崇拜孔丘关羽，还不如崇拜达尔文易卜生。”<br />
<br />
1921年，李璜等中国青年党人给巴黎大学的学者写信，请教三个问题：一、人是否宗教的动物？二、新旧宗教是否还有存在的价值？三、新中国是否还要宗教？他们收到了三位著名教授的回答，他们是中国历史专家家马尔塞&#8226;；格那列、小说家亨利&#8226;；巴尔比斯、社会学学者布格列，他们的回答都是否定的。巴尔比斯认为，基督教作为经济政治扩张的工具进入中国是个不幸。<br />
<br />
罗素把马克思主义当作基督教、佛教、伊斯兰教之外的另一种宗教来加以批判，李大钊也曾同意布尔什维主义是一种类似于宗教的群众运动。<br />
<br />
1924年，辜鸿铭赴日本讲学，认为中日之争为兄弟阋墙，而日本之所以能御侮于外，非明治西化之故，实中国传统之功。西方人言性恶，互为猜忌攻伐，衍为欧战，若听任之，必成人类之浩劫。日本若持续西化，亦必将扰乱东方，因此应喝止西化，荷担起复兴东方文明的使命，进而拯救世界。日本之喝止西化，亦足以喝止中国之西化，日本须将纯正的中华文明反馈中国，这是上天给予日本的使命。这就是所谓大东亚文化建设之论，日本人因之而成大东亚的政治理论。<br />
<br />
1929年，日本首相田中义一在上呈天皇的奏折中说：“如欲征服中国，必先征服满蒙；如欲征服世界，必先征服中国。”<br />
<br />
梅兰芳访美时，一位美国老太太十分痴迷，每场必到。后来她得到了与梅兰芳见面谈话的机会，她拉着梅兰芳的手赞叹不已，说：“这是多么好看的一双手呵！为什么演出的时候要用袖子遮起来呢？”<br />
<br />
何键离任湖南省主席前，断的最后一个案子是，下令枪毙了一个妇女，理由是她竟然嫁给日本商人为妾。那日本人已经撤侨回国，他的中国小妾不仅有“通敌罪”，且有辱中华女性的民族气节，所以罪不可赦。自然不少人拍手叫好！然而不要忘记，中日开战后，郭沫若回国当了高级官员，他留在日本的妻儿纵然遭受歧视，也决不至于被判刑或处死。可见中国当时的法律精神是多么淡薄了。<br />
<br />
抗战爆发后，陶行知赴美国募捐，结识了加拿大蒙特利尔皇家维多利亚医院的著名脑外科大夫白求恩。陶行知向白求恩介绍了中国的严峻形势，白求恩便马上组织了一支医疗队赶赴中国。<br />
<br />
抗战中，陪都重庆电力缺乏，灯光昏暗，可即使是这昏暗的灯光也是有限制的。对此蒋廷黼在行政院会议上提出采取日光节约时间，每年4月1日将钟表拨快一小时，行政院长孔祥熙当即反对，认为这是闻所未闻的。此议于是被否定。但是不久以后，一个美国人提出了相同的建议，却马上被采纳了。<br />
<br />
抗战后，北平行营主任李宗仁邀请驻华美军司令魏德迈将军，请洪业作陪。魏德迈致词：“中国之所以未能成为强国，对世界和平及繁荣有所贡献，乃由于两大敌人的阻碍。一是日本，半个世纪以来一直操纵中国政治，给中国带来灾难，现在中国在美国帮助下已经打败了日本。第二个敌人是你们的内奸，我们美国人爱莫能助，这个内奸的名字就叫贪婪。你们若要享受真正的自由，要为人类的福利尽一份力量，非得除去这个内奸不可。”给魏德迈做翻译的是另一位美国将军普利士，他没有翻译魏德迈的第二段话。这时洪业站起来说：“我以平民和历史学家的身份，向两位将军致谢。魏德迈将军说得好极了，普利士将军翻得也很准确，但他为了给我们中国人留面子，第二部分没有翻完，现在我来把它翻完。”他的举动只赢得了一片沉默。<br />
<br />
抗战后，美国为着自己在远东的利益，极力调停国共矛盾。1946年2月，当国、共及其他各党派在重庆上清寺签订一个基本方案后，美国特使马歇尔致词：“此协定为中国之希望。我相信其将不为少数顽固分子所污损，盖此少数顽固分子，自私自利，即摧毁中国大多数人民所渴望之和平及繁荣生存权利而不顾也。”<br />
<br />
毛泽东到莫斯科为斯大林祝寿，斯大林非常激动，对毛赞不绝口：“伟大，真伟大！你对中国人民的贡献很大，你是中国人民的好儿子！我们祝你健康！”毛回答道：“我是长期受打击排挤的人，有话无处说……”斯大林打断他的话：“胜利者是不受谴责的。不能谴责胜利者，这是一般的公理！”<br />
<br />
1949年，国民党在大陆的颓势一发而不可收，胡适、雷震等人办了一份杂志，希望借文字之功，鼓舞灰心丧气的军民，挽大厦于将倾。胡适提议，当年戴高乐在国难中办有一份《自由法国》，那么现在的这份杂志不妨叫《自由中国》。雷震即向蒋介石转述这层意义，蒋对其大加嘉许。但等他们都移驾台湾之后，这份刊物却成为攻击国民党独裁统治的利器，最终被封禁了事。<br />
<br />
雷震案发生后，美国的中国问题专家费正清在《纽约时报》上质问美国政府：“我们广大的援助背后，竟没有政治自由的意念吗？我们冒了战争的危险保障台湾，只不过是为了支持一个宁愿使用不必要的警察国家方法，而不愿意健全的政治进步的独裁政权吗？”<br />
<br />
中国从英国进口斯贝发动机，英国人感到很奇怪，因为斯贝发动机是中国人吴仲华发明的。吴仲华20多岁就在英国当了教授，在日内瓦会议时见到了周恩来，要求回国，虽然英国方面拼命挽留，他却义无反顾。他回国几十年内都默默无闻，还不断受到批斗。后来国家发现进口的斯贝发动机有质量问题，便派他去交涉，英国人听说他来了，马上同意照价赔偿，不少英国专家还声称吴是他们的老师。<br />
<br />
文革中，商店招牌大多改为“工农兵”、“红星”、“延安”、“韶山”之类的革命名称，此外还要在合适地方写上“为人民服务”、“毛泽东思想万岁”等口号，即所谓“一片红海洋”，外观上根本看不出是卖哪种商品的。尼克松访华时，北京的“红海洋”突然消失了，很多商店恢复了原来的招牌，这也算是尼克松对中国的一大贡献吧。<br />
<br />
1972年，中美在杭州谈判，因在对台湾的表述上存在重大分歧，谈判陷入僵局。谈判闲暇，基辛格指着西湖的苏堤问：“那是什么？”乔冠华答：“那是苏堤，是1000多年前的一位杭州市长苏东坡修的。”基辛格，又问：“这一边是西湖，另一边呢？”乔答：“也是西湖。”基辛格忽有所悟，遂与中方达成共识。次日，《中美联合公报》在上海发表，其中有云：“美国认识到，在台湾海峡两边的所有中国人都认为只有一个中国，美国对这一立场不提异议。”<br />
<br />
杜维明对唐德刚说：“近百余年来，我们都在向西方学习。现在他们的好东西我们都学会了，他们也该学学我们的好东西了。”</div>

							
							
							
							
