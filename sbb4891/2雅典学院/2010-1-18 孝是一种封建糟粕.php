关于孝顺，昨天我想了一夜，突然有了一些新的想法。<br />
<br />
我觉得，对于一件事来说，有对有错。当然不同的人可能对于事情的对错有不同的看法，但是不管怎样，绝大多数的人都会去做对的事情，而不做不对的事情，只有孝道不是这样的。孝道抛弃了是非观念，如果是你的长辈，你就要听他的任何话，不管对错。中国人奉行“仁义礼智信,忠孝廉耻勇”，如果谁没有做到，就是大逆不道，道德败坏，为人所不齿，所以几千年来没几个人能越过这个雷池。<br />
<br />
其他几个词我没怎么研究，但是孝的缘由，我想应该是这样的。中国人普遍贫穷，种田的就不用说了，生老病死没有保障，达官贵人虽然有钱，但是谁也预料不到那些突发的事。所以有句话叫，“养儿防老”，什么意思？就是我年轻的时候养你，给你吃给你穿，老了生活没有依托了，就让儿女们照顾，解决养老问题。比较可悲的事，养老问题几千年后到现在居然还是没有解决。<br />
<br />
孝，起源于养儿防老，但是不止养儿防老。可以说，孝是封建君主为了逃避责任建立的一种特殊的养老制度。其目的就是以家长为中心，随意践踏儿女们的想法，为自己的个人意志而服务。这本身就是过去父母依仗着自己处于强势、孩子处于弱势而制定的极不公平的强权、霸权主义。比如，我跟我老人们有时候会有意见不一致，每当这个时候，我就会把自己的想法搬出来跟他们理论，因为我觉得我是对的，既然是对的，那就要坚持。但是我爸就会说，他们年纪这么大了，不要不懂事，不要去跟老人们争。其实我也不想跟他们争，老人们确实比较顽固。我觉得让我嘴皮子上不争可以，但是如果在做事上不争就太不应该了。毕竟我做事又不是你们做事。但是其实“争”本身就已经是不孝顺了，“听话”才叫孝顺。<br />
<br />
我不知道这种封建糟粕毒害了多少人，多少人因为一个“孝”字放弃了自己的理想，放弃了自己的事业，或是放弃了自己的爱人，一辈子去做一些自己不喜欢做的事情，没有理由，仅仅是因为“孝”这个字。<br />
<br />
有人说，父母一把屎一把尿把你喂大，你要尽自己的养育之恩。老罗说过，父母儿女无恩论，我绝对支持。是父母想生我，不是我自己要出生，来这个世界的，为什么我要报答？父母养育儿女是义务，不养就是犯罪。<br />
<br />
我反对孝，但是我爱我父母，我也会尽力让他们有一个幸福的晚年。我希望所有人在处理和父母问题上用爱来解决。爱意味着你是发自真心的做事，意味着你有听父母的权利，也有不听的权利。你觉得对，你就去做，你觉得不对，大可不必去做，因为这是你的生活，不是你父母的。<br />
<br />
我想在国外应该没有“孝”这种事吧，人家养老制度那是相当的优越。<br />
