&nbsp; &nbsp; “孝”这个字，上面一个“土”，可以意为“天生的”，一撇下面一个“子”，仿佛一把刀压在“子”头上。又可以理解为半个“老”压着一个“子”。整个字的意思就是做子女的天生就得顶着来自长辈的一把刀，受着长辈的压迫，长辈可以犯错误而不道歉，子女却只能逆来顺受。<br />
<br />
&nbsp; &nbsp; 所谓忠孝仁义,本身就是封建君王用来维护统治的。儒家思想里的君为臣纲,父为子纲,夫为妻纲（含义：要求为臣、为子、为妻的必须绝对服从于君、父、夫）是很独裁的.<br />
<br />
&nbsp; &nbsp; 如果真的爱小孩的话，按照现行状况，很多人应该选择不生，而不是生。人一出生就开始面对疾病，生活，压力和死亡。有幸福的人，但是也有非常大的一部分是不幸福的。所以不是所有的人都适合要孩子的，要看你有没有生养、教育孩子的能力。<br />
<br />
&nbsp; &nbsp; 受过良好家庭教育的孩子是绝对不会不孝顺父母的。受过良好家教的孩子不仅孝敬父母，而且和父母的关系非常融洽，几乎是无话不谈<br />
<br />
&nbsp; &nbsp; 那些不被孩子孝顺的父母纯粹是自己造成的。<br />
&nbsp; &nbsp; 可怜之人必有可恨之处，养不教父之过，任何事情都是有原因的。这样的父母应该好好反省一下自己，想想以前都是怎么对待孩子的，给孩子造成了哪些伤害。不过这样的父母大多从来不反省自己，即使反省也找不到原因，因为这样的父母根本就是无知、至少在教育孩子这方面根本不懂<br />
<br />
&nbsp; &nbsp; 中国有很多父母不仅仅是不懂教育，而且人格有缺陷、心理不健全<br />
<br />
&nbsp; &nbsp; 不懂教育，指的是教育方法的不科学、不合理；但是中国有些父母对待孩子的方式根本就谈不上是教育，至少打骂孩子就不是教育，而是暴力行为，打骂只能在外表上、肉体上控制孩子，但是不能让孩子内心得到服从。从某种程度上讲，打骂孩子是一种变态行为（象征性的打骂、以讲道理为主的除外）。另外打孩子是违法的，《未成年人保护法》有规定，但是中国的法制不健全，很多东西都是一个摆设；父母要是在美国等发达国家打孩子，早被警察抓走了。<br />
<br />
&nbsp; &nbsp; 打骂孩子、羞辱孩子、（有些甚至当着外人的面打骂、羞辱孩子）、拿自己孩子和别人家的做比较、随便对孩子发脾气、对孩子大声吼叫、不讲理、答应孩子的事做不到、把他们自己认为正确的方法思想强加给孩子而不顾孩子的感受……，以上这些根本就不是什么教育方法。这样的父母已经不是不懂教育的问题了，而是人格有重大缺陷、心理也不健全。<br />
<br />
&nbsp; &nbsp; 动不动就抱怨养孩子多么不容易，我承认，父母养一个孩子确实不容易，但是话又说回来，自己生的孩子自己不养，让谁去养？先别说人了，就是自然界的很多动物和野兽也是这么做的，“虎毒不食子”，像老虎、狮子这样凶狠的野兽生了孩子也一样把它养大。养自己的孩子根本就是动物的一种本能，对人来说更是一种责任和义务，所以养大自己的孩子是理所应当、天经地义的，没什么好骄傲的。如果说嫌养孩子麻烦，那完全可以不生呀，谁让你生了？这不是自找的吗！中国有很多父母对孩子还只停留在“只养不教”上，没有养育、教育孩子的能力就别生孩子，否则不仅伤害孩子，而且也害自己。<br />
<br />
&nbsp; &nbsp; 还有些父母会说：我生了你，给了你生命，你就应该怎么怎么样。这叫什么封建谬论？搞清楚：是谁让父母生的孩子？是他们自己。孩子并没有要求父母生，也没法要求父母生。即使父母怀孕了，突然不想要孩子了，也可以到医院把孩子打掉，生不生孩子完全是父母说了算，孩子能否来到这世上是自己决定不了的，从这一点来说，虽然父母给了孩子生命，但是孩子并不欠父母的。而且作为孩子，也未必想来到这个世上，人一出生就开始面对疾病、生活、压力和死亡，人活在这世上很不容易的，有很多时候都是在受苦（包括肉体上和精神上）。退一步说，就算孩子真的想来到这世上，也未必想生在你家里，任何孩子都想出生在一个幸福美满的家庭里，先不说富裕贫穷了，即使是生在贫穷的家庭里，孩子也希望有个懂得教育的父母，至少也希望父母的性格脾气温和，没有哪个孩子愿意让一个脾气暴躁、打骂孩子、又不懂道理的人做父母。但是这一切孩子是无法选择的，生在什么样的家庭里那完全是运气，作为孩子，永远只能被动的、无奈的接受一切，孩子永远是无辜的。<br />
<br />
&nbsp; &nbsp; 父母心情不好、工作生活压力大可以理解，但是不能对孩子发泄，因为孩子是无辜的。父母发泄完自己是舒服了，那孩子被父母骂完受的气冲谁发泄去？就没想过孩子的感受吗？孩子在父母眼里难道就是个出气筒？中国有句古话叫：“子不嫌母丑”，就是说孩子不能比较父母的好坏，但是父母却可以嫌弃自己的孩子不如别人家的好，这本身就是过去父母依仗着自己处于强势、孩子处于弱势而制定的极不公平的强权、霸权主义，毫无人权可言。自己孩子有出息了，就在外边到处炫耀；如果孩子暂时没什么作为，就骂孩子是废物、窝囊废，就拿别人家的孩子来做比较。孩子完全成了父母的一个工具、一个傀儡。大家说这样的父母是不是人格、心理不健全？这样的父母配让儿女孝顺吗？<br />
<br />
&nbsp; &nbsp; 中国还有句话叫：“养子防老”，就是说父母生养孩子完全是为了自己的养老，父母老了如果子女不在身边就被指责为不孝顺；而西方父母却把养孩子当作享受天伦之乐，父母把孩子养大后大多是自己独立生活，不能自立的就去养老院之类的一些地方，孩子很少有和父母一起生活的。在这方面应该多多学习西方的先进教育思想，在西方父母和孩子之间都是讲求互相尊重、互相平等的，而且崇尚鼓励孩子自由发展……<br />
<br />
&nbsp; &nbsp; 同样是为人父母，为什么中国和西方相差如此之大，这也算是一中国特色吧。<br />
<br />
&nbsp; &nbsp; 出生在这样家庭里的孩子，从小到大都要饱受精神上的折磨，长期下去很容易产生心里扭曲，严重的甚至会心理变态、发疯、精神分裂。这样的孩子在学校、社会上不知道要吃多少亏，等明白醒悟过来早已是遍体鳞伤了，肉体上的创伤容易愈合，但是心灵上的创伤―心理阴影恐怕要伴随自己一生。有心理专家数据统计，90%以上孩子的心理问题和家庭有关―C主要源于父母的性格、脾气不好，用自己的想法控制孩子，与孩子的沟通方式不公平，家庭暴力（打骂孩子）……只有不到10%的心理问题是来自学习、工作等社会外界的压力和社会上人的相处。<br />
<br />
&nbsp; &nbsp; 有些孩子不孝顺、离家出走、骂自己的父母、甚至有些还杀死父母等等，一些人看到这种情况的第一反应就是先责怪孩子，无风不起浪，天下就没有无缘无故的事情，难道你们就不会反思一下原因吗？之所以不理解，只能说明这样的人成长在比较幸福的家庭里、或者这些人的家庭即使不合格但是还没有糟糕到那种地步，家家有本难念的经，每个人的家庭环境都不一样，很多事情必须亲身经历才能有切身体会。考虑问题不能只站在自己的角度，要学会换位思考。那些人也非常希望有一个好的家庭，如果可以的话，谁又愿意对自己的父母那样呢？<br />
<br />
&nbsp; &nbsp; 教育方法有缺陷的父母有救；但是人格有缺陷、心里不健全的父母==不可救药。<br />
<br />
&nbsp; &nbsp; 对于出生在以上家庭的孩子来说真的是很不幸，但是现实无法改变，只有自强自立好好奋斗，尽快远离父母争取独立，才是孩子的唯一出路。先天的命运无法选择，但是后天的命运可以自己掌握！<br />
<br />
&nbsp; &nbsp; 至于有些人说什么要和父母努力沟通什么的，说实话根本没有用，首先说这种方法的人肯定不是在不合格家庭中成长的孩子。那些在合格家庭里长大的幸运孩子是很难理解的。<br />
<br />
&nbsp; &nbsp; 所以对待这种家长的唯一方法是：“尽快独立，远离他们”，跟着这种父母混真的会毁了自己一生。<br />
<br />
&nbsp; &nbsp; 中、西方父母生养孩子的最大区别<br />
<br />
&nbsp; &nbsp; 在西方，父母创造一个生命来到这世上是为了享受天伦之乐。而对于绝大多数的中国人来讲,他们生育的最大目的，是为了养一个子女来保障他们将来的生活，也就是所谓的“养儿防老”。“养儿防老”，译成经济化的语言，也就是“投资”，也就是说，中国人生育子女，其实是在作投资！！<br />
<br />
&nbsp; &nbsp; 所以，我们又可以理解了，为什么中国的生育率始终下不来？为什么中国的人口难以得到有效控制？为什么人们即便没有能力抚养子女，让子女过好的生活，接受好的教育，却依然还是要惨无人道地拼命生养呢？因为他要追求投资保障的最安全化和投资收益的最大化！悲哀啊！！！！！</blockquote></div>[<i> 本帖最后由 低调の华丽 于 2010-1-18 15:52 编辑 </i>]</div>

							
							
							
							
