													<h2>回教世界与中国何以友好</h2>
						
						
																											<div id="postmessage_424808" class="t_msgfont">哈佛大学亨廷顿教授（Samuel Huntington，1927～）在其《文明的冲突》一书中，说害怕中国和伊斯兰世界结盟，从中共年来对回教世界的武器供应，以及中共与回教国家之间如胶似漆的亲昵表现，这个忧虑确是甚有道理的。<br />
<br />
但这现象实在耐人寻味。查中国对回教徒的残害与侮辱，罄竹难书，世所罕见，清朝为镇压西北部的回乱，铲平回教汗国，先后屠杀回民以千万计，令十字军东征中死难回民之惨烈也相形见绌，令以色列的暴行也显得琐碎。中共掌政，长期残暴镇压西北部回民的立国或复国运动，文革期间更曾鼓励回民吃猪肉，强迫回民养猪，关闭清真寺，甚至强迫回民仿猪爬、学猪叫，甚至戴猪头，挂猪肠子游斗。<br />
<br />
再以整体文化格局而言，中国人彻底世俗，宗教精神荡然，一切利字当头，万事以吃为先。回教视猪狗和病死动物为“不洁”，生人勿近，中国人居然通通放进嘴里大嚼。一个笃信无神、多神或泛神，一个坚信一神。一个不准借贷附利息，一个是高利贷之邦。论情操，一个贪生怕死，或为财而死，一个为了宗教信念视死如归，连中国的残暴狡诈也无法将之根灭。<br />
惟其如是，伊斯兰世界还是看来与中国异常友好，除了美国这个共同敌人令他们团结起来之外，到底还有什么原因？<br />
<br />
践踏人权声气相投<br />
<br />
伊斯兰世界和中国在某些很重要的价值观上出奇一致，譬如对人权、民主、平等等概念的蔑视。别的不说，伊斯兰世界对妇女的压迫，和中国对老百姓的压迫，已经难以见容于现代文明世界。虽然无数中国人仍然执信“天下无不是之父母”，但现代文明世界也已经不容许父母虐待子女，也不容许政府虐待国民。“家有家规，国有国法”的中世纪奴隶主法宝已经失效。<br />
<br />
伊斯兰世界和中国同样以中世纪的价值观勉强拐进现代文明世界，举步维艰，很不容易找到一个完全不会理会自己虐妻待儿、为所欲为的朋友。这实在是很美妙的享受，对于挽回过气世界盟主的一点点尊严和自我，尤为重要。<br />
<br />
这就是所谓“不得干预别国内政”的意义所在。但“不得干预别国内政”也很容易发挥到令人啼笑皆非的地步，譬如俄罗斯血洗信奉回教的车臣，波斯尼亚、科索沃的回教徒惨被种族清洗，中共仍以“不得干预别国内政”为由，袖手旁观，见死不救，而且还豺狼塞路。同样讽刺的是，搭救这些回教徒的居然是伊斯兰世界视为死敌，并且是信奉基督教的美国。<br />
<br />
过去，伊斯兰世界跟中国同样发展了一些相当灿烂的科技文明，谁不肃然起敬，焚香顶礼呢？但两者却始终发展不出、也拒绝移植自由、人权、宽容这些精神文明，结果是糟蹋了无数技术发明。伊斯兰世界跟中国都因为欠缺了这些精神文明而由盛而衰，同样两者至今都没有对其死因深刻反省。从这一点看，这两个文明却又是性相近，大有物以类聚的先天条件的。<br />
<br />
中国领导两面人误导回教领袖<br />
<br />
伊斯兰世界居然和中国异常友好的另一个原因是，中国人，或者说是中国领导，热衷于弄虚作假，演猴子戏。这一点过去我已略有论及，最近在克林顿刚出版的自传中再得印证。克林顿在书中这样推崇江泽民“他迷人、风趣……总会聆听不同意见”（He was intriguing，funny…always willing to listen to different point of view.）<br />
<br />
香港人恐怕不会有机会觉得江泽民风趣和乐于聆听不同意见，到会想起他大骂香港记者“听风就是雨”“闷声发大财”，too simple sometimes naive时的凶相。即使是深受当局洗脑的大陆民众，相信也不会对江泽民有克林顿这样的正面评价。但我相信克林顿这样说不尽是外交辞令，也是发自内心。原因是中国领导人素来有一传统，就是对着国民扳起脸孔，呼奴喝婢，对着洋人就嬉皮笑脸，讨洋人欢心。<br />
<br />
江泽民敢捶胸顿足的教训美国记者吗，教训访问他的华莱士（Mike Wallace，1918～）吗？江泽民会见克林顿时敢不装得泱泱大度、侧耳聆听克林顿的意见吗？你什么时候会看见江泽民对着中国人嚷着要乱弹琴，作天才表现？又或听到他在中国人面前朗诵林肯讴歌“民有民治民享”的盖茨堡献词？<br />
<br />
连精明、直言得多的美国前国务卿欧布赖特（Madeleine Albright，1937～）也只是略带尖酸的说江泽民“像《爱丽丝梦游仙境》里面的柴郡猫，常咧嘴而笑，好想倾倒众生，却弄出几分奸相来。”就是说，他们即使不觉得江风趣，也仅觉得他滑稽而已。这与我们对江泽民的负面印象还是颇有距离。<br />
<br />
在洋人面前，特别是在美国人面前，连神圣不可侵犯的邓小平也得哈腰卖乖，欣然戴上一顶大得惹笑的牛仔帽向记者扮鬼脸，矮化自己，美国民众怎能现象这就是囚禁魏京生20年、杀人不眨眼的暴君？他们怎会相信世界上会有这样一个虚伪做作的民族？在公开场合尚且如此，关起门来怎样吮痈舔痣、“有辱国体”真的不敢想象。<br />
<br />
毛泽东关起门来就对日本的访问团说不用为侵略中国感到内疚，遑论道歉。领导人或讨或逗平民开心，与民同乐，只会发生在外国，中国民众是没有这个福分的，因为“宁予外族，不予家奴”仍然是中国政治的铁律。中国领导曲意逢迎外国人的伎俩，外国人是极难识破的，这对促进外国政府与中国友好，作用极大。<br />
<br />
中国统治者历来虐待同胞，善待外人，令即使是广受推崇的人权斗士如曼德拉，也不得不像玉通和尚被红莲“破身”那样，拥护起比南非白人政权更野蛮的中共政权来。中国一手打压新疆争取摆脱暴政或复国的回民，另一手嬉皮笑脸向回教国家领袖讨好卖乖，寻常回教领袖又怎能洞烛其奸？<br />
<br />
还是因误会而结合<br />
<br />
但要是伊斯兰世界真的与中国那样水乳交融，为什么印尼和马来西亚的回教徒与华人的关系那样恶劣呢？看看奈波尔（V.S.Naipaul,1923～）在其大作《在信徒的国度：回教之旅》中在这两地的游历和深度文化探讨，就可见即便是较世俗的印尼和马来西亚的回教徒也是跟中国人“同住难”的。<br />
<br />
当然，中国境内的回教徒也是难以与中国人同住的，但中国的社会控制和资讯封锁远比印尼和马来西亚严厉，要了解伊斯兰文化和中国文化相遇的境况，观察印尼和马来西亚容易得多。<br />
<br />
也许有人在传媒上看到以色列或美国治下的回教徒很惨，但要是统治以色列和美国的是中国人，相信那里的回教徒连被传媒报道的机会也没有！因为一切都是“国家机密”！可笑的是，许多人，当中甚至包括回教徒，就是因为美国容许报道回教徒对美国迫害的指控，而中国则严禁报道回教徒对中国迫害的指控，而相信美国就是回教徒的头号敌人，而中国就是回教徒的老朋友。<br />
<br />
看来伊斯兰世界之所以能够普遍和中国保持友好，一个很重要的原因就是中国人没有大批移民到这些回教国家，以致双方都没有机会深入了解对方，继续作搔不到痒处的蜻蜓点水式文化交流。彼此继续雾里看花，保持距离，是中国和伊斯兰世界进一步结盟的必要条件。就如中国的邻国很容易讨厌中国，你去问一问印度、日本、台湾、菲律宾、印尼、越南等国的人就知道，但千里之外，连中国、台湾也无从分辨的国家却每每美化中国那样，都是离不开“因误会而结合，因了解而分开”的人间定律。</div>

							
							
							
							
