By Fong Tak-ho<br />
<br />
HONG KONG - Over the past 16 years, the Chinese leadership has tried its best to dodge democratic reform while looking for alternative measures to stamp out rampant corruption and increase government efficiency. However, it seems to have recently come to the conclusion that there is just no way other than democratic reform.<br />
<br />
Chinese President Hu Jintao has indicated that China will institute a program of democratic reforms, and Premier Wen Jiabao has given more detail, pledging to introduce direct elections at the township level &quot;within a couple of years&quot;.<br />
 and Wen chose to make their announcements during international events shorthly before Hu's trip last week to the United Nations summit meeting in New York. This could be a sign that both are eager to project a reformist image to the international community as part of efforts to defuse the theory of a &quot;China threat&quot; from what is still officially a communist government.<br />
<br />
&quot;China will press for democratic progress, unswervingly reestablish democracy, including direct elections,&quot; the premier, who favors mild reform, told a news conference prior to the 8th EU-China summit on September 5. &quot;If we Chinese people can manage a village, I believe they can manage a town in several years. This system [of direct voting] will be realized step by step.&quot;<br />
<br />
Visiting British Prime Minister Tony Blair, who sat next to Wen, was reportedly &quot;shocked&quot; when he heard the premier's remarks. Blair shouldn't be surprised. What Wen is talking about is only a mild democratic reform package. Similar political schemes were introduced as early as 1988.<br />
<br />
Former Chinese leaders, such as late Communist Party party chiefs Hu Yaobang and Zhao Ziyang, considered what Wen is now planning. Such initiatives came to a sudden halt in 1989 when Hu Yaobang passed away, three years after the pro-reform leader was sacked from his post, which immediately sparked an outcry for more democracy. This pro-democracy campaign culminated in the showdown at Tiananmen Square on June 4, 1989 and the replacement of Zhao Ziyang by Jiang Zemin.<br />
<br />
During Jiang's era, direct elections were introduced to Chinese villages. Nevertheless, critics said this was far from enough.<br />
<br />
The Chinese government has now exhausted other measures aimed at maintaining social stability, and all are considered to have been unsuccessful. Former premier Zhu Rongji's state-owned enterprise reforms, for instance, backfired as the procedure for management buy-out resulted in even more corruption and embezzlement. (The leaders all agreed that economic reform had a strong political aspect.) Administrative measures alone have proven a poor substitute for democracy.<br />
<br />
When China was under Jiang's rule, corruption got worse as individual corrupt officials collaborated with each other, and in recent years the central government has exposed corrupt syndicates involving hundreds of officials.<br />
<br />
Meanwhile, many local officials have made use of their unchecked power to exploit natural resources. They have received kickbacks from property developers, and they run coal mines without considering the impact on the environment. Beijing realizes it could face an ecological disaster should this trend remain unaddressed.<br />
<br />
When the Jiang administration was replaced by Hu Jintao's team in 2003, Zhao's former secretary, Wen Jiabao, became premier. Now, it looks as if Wen is determined to finish what his former boss could not.<br />
<br />
Before Wen declared his intention to introduce more democracy, he tried to introduce administrative measures to lift the efficiency of his civil service. He also issued a spate of government decrees stressing the need to address grassroots problems.<br />
Most government officials, however, turned a deaf ear to these repeated calls. The reason is simple: officials in China are appointed, instead of being elected, and they are not accountable at the grassroots level.<br />
<br />
For a long time, Wen has been trying hard to reduce redundancy, to streamline the bulky administrative structure. Like previous streamlining efforts, the plan to cut down the size of the government has met with a huge amount of resistance. To change this situation, Wen has to make sure that the grassroots' voices are heard.<br />
<br />
Another reason Wen has to introduce democracy at the township level is to check the power of provincial leaders. These leaders are at the top of the executive, judicial and legislative wings in local government, while theoretically they also have command of the army in their areas.<br />
<br />
A provincial leader's power is essentially unchecked. Under such circumstance, the Chinese central government has in the past empowered city and township governments with financial autonomy to prevent the provincial government from becoming too powerful. But this measure has not been very effective as city leaders also can be corrupt.<br />
<br />
Thus, the central government is contemplating scrapping the city's role in controlling the city's budget. However, a new problem arises. There are thousands of cities and towns in a single province, and it is very hard for the province to supervise all their operations. Thus the central government is now convinced that corruption at the township level can only be checked by democracy being introduced at that level.<br />
<br />
Now it is a question of timing beyond the broad &quot;within a couple of years&quot; promise. No detailed timetable has been announced. In the most optimistic scenario, this reform could be incorporated in the working report for the 2007 17th Communist Party congress. Should Wen's idea meet great resistance, the issue could be delayed to the 18th congress in 2008. In both scenarios, Wen's promise of introducing reform &quot;within a couple of years&quot; could be kept.<br />
<br />
There appears to be a rhythm of reform in Chinese history since modern-day China came into existence in 1949. Apart from the unusual period of the Cultural Revolution from 1966 to 1976, a wave of reform thinking washes over China every 14 years or so.<br />
<br />
Taking over the mainland in October 1949, the Chinese government virtually ran the state from 1950. In 1964, when most of Mao Zedong's planned economic policies had failed and resulted in the massive famine of the late 1950s, former president Liu Shaoqi took over command of the economy and introduced some elements of market reform. Liu's reform package, however, was called off when Mao initiated the decade-long Cultural Revolution in 1966.<br />
<br />
After this devastation, the late leader, Deng Xiaoping, won over his conservative rivals in 1978 to launch his open-door policy. After another 14 years in 1992, Deng pressed ahead with further market reforms by visiting the Special Economic Zone of Shenzhen. Now, it has been almost 14 years since Deng's call to spearhead further market reform.<br />
<br />
This 14-year phenomenon is not merely a coincidence. Fourteen years is long enough for the government to forget the very spirit of reform, while it is also long enough for new problems to arise. Officials need to be reminded why there was a need for reform in the first place. The people need to be told how problems that appeared following the previous bout of reforms can be fixed - and this is by further and harder reform.</div>

							
							
							
							
