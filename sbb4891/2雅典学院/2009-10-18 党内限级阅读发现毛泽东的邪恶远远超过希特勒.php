													<h2>党内限级阅读发现毛泽东的邪恶远远超过希特勒</h2>
						
						
																											<div id="postmessage_287996" class="t_msgfont">【 阿波罗新闻网2009-10-17讯】 作者：张成觉 　&nbsp;&nbsp;<br />
<br />
这个结论是我远在三十多年前得出来的。 <br />
　 <br />
1973年左右，我有机会读到一份中共内部文献，名字大概叫《毛泽东选集/内部》，由中共中央毛泽东选集编辑委员会选编，党内限级阅读。所选编的文献全部是中华人民共和国成立后，直至六零年左右，毛泽东在中央高层的历次重要讲话。出版日期应该是在四清运动开始前后。书的厚度比公开发行的毛选四卷的一卷本略薄。 <br />
<br />
选集中的许多讲话过去都略有所闻，没有引起我太多兴趣。有两篇给我印像比较深，一篇是58年关于大跃进的讲话，毛说U我们在报纸上公开提出超英赶美，十五年超过英国的口号，其实我们十五年完全可以超过美国，我们公开只提十五年超过英国，是说话留有余地;另一篇是59年毛关于把国家主席让给刘少奇的讲话，从那篇讲话中明显可以判断，毛泽东是在党内巨大压力下不得不把国家主席的位置让给刘少奇。 <br />
<br />
选集中令我十分震惊的，是毛泽东关于第三次世界大战的几次讲话，读过这几篇讲话，我当时的结论就是U毛泽东的邪恶，远比德国法西斯希特勒有过之而无不及。历史如果给了毛泽东机会，中华民族肯定早已经被他毁灭了。 <br />
<br />
我当时推想这本选集的出版可能与毛刘两个司令部的斗争有关，可能是以刘少奇为首的一派人有意假借宣传毛泽东思想之机，扩大共产党内对毛泽东的反人民思想的了解，为日后的党内斗争做铺垫，却没有想到最后还是被毛占了先机，落得个死无葬身之地的下场。 <br />
<br />
毛泽东关于第三次世界大战的这几次讲话均集中在1955年，其时代背景是抗美援朝“胜利”后，赫鲁晓夫公开批判斯大林之前。讲话可能有3-4次之多。概括起来其主要内容是U <br />
<br />
　 1）世界大战并不可怕。第一次世界大战打出来一个社会主义国家苏联，第二次世界大战打出来一个社会主义阵营。第三次世界大战如果爆发，就可以实现世界大同。 <br />
<br />
2）第三次世界大战应该早打，大打，打核战争，在中国打。 <br />
<br />
3）第三次世界大战如果爆发，我建议苏联假装坐观，由我来带领中国人民把美国军队吸引到中国战场，我们同美国军队打常规战。战争扩大滚雪球，然后我们假装败退，逐步把美国军队引入中国内地，使美国军队陷入人民战争的汪洋大海中，从而迫使美国向中国战场投入主力军队。当美国将主力军队投入中国战场后，请苏联向中国战场突然投射原子弹，将美国主力军队一举歼灭在中国的战场上。 <br />
<br />
4）这样的一场世界大战中国可能会死掉四亿人口。但是中国用三分之二人口的牺牲，却换来一个大同的世界还是值得的。 （当时中国的统计人口是六亿） <br />
<br />
5）死掉四亿人，还剩两亿人，用不了多少年，中国就又可以恢复到六亿人口了。 <br />
<br />
这真是让人难以置信U中国人预计死四亿，那全世界要死多少亿呢？相比之下，二次世界大战全世界共死了三千多万人，不过是毛泽东大手笔的零头。而且二次世界大战死那么多人未见得是战争狂人在战争前的预计和初衷---因为在发动战争前，战争狂人无不把战争胜利的获得想得很轻松，陷入战争泥潭不能自拔才是无休止地杀人的进一步原因。再说希特勒和日本鬼子要杀死的是外族人，而毛泽东首先预计的是死自己的同胞。四亿人，那可是预计要牺牲的自己的同胞啊，战争一旦爆发，成了脱缰野马，六亿中国人岂不是全搭进去了？中国人岂不是死绝了！足见其其内心邪恶远非前者所能比拟。 <br />
<br />
当我读到毛泽东的这些讲话时想到，毛泽东大权在握，真是得意忘形了，这样的话，想一想都是罪大恶极，他竟然还敢在中央高层会议上讲出来！尽管共产党残忍，那些高干尤其如此，但是我相信任何承认自己还有中华民族血脉的人，哪怕是土匪，也会骂毛泽东民族败类，狼子野心了，只不过在其淫威下不得不隐藏自己的观点。我是所谓在红旗下长大的一代，共产党毛泽东在我的心目中曾经完美无瑕。争三好，学雷锋，加入少先队，共青团，直至大学毕业前夕加入共产党。因为那时不间断的政治运动，连续的杀、关、管、教，我们那一代的父母都已经成了惊弓之鸟，没有人还敢向孩子们说真话，只能任由学校，报纸，广播，向青少年们进行随心所欲的谎言灌输。所以共产党对我们那一代人曾经进行了空前绝后的，最成功的谎言教育。 <br />
<br />
青少年时代虽然也经历了荒谬的三面红旗运动，饥饿的三年困难时期，甚至祸国殃民的文化大革命，对毛泽东曾有所怀疑，但仍然不愿意从内心深处否定他，愿意原谅他。我对毛泽东认识的质变就发生在我读到这本内部毛选后，知道毛泽东曾经不惜用四亿中国人，三分之二中国人的生命为代价，为他换得大同世界领袖的地位。试想一下，如果一个自称是人民的大救星的领袖，预谋让他的国家里每一个三口人之家都死掉两口，那个人是人民的大救星还是魔鬼？当然是魔鬼！ <br />
<br />
中国有句古话说，皮之不存，毛将焉附。中共和毛泽东谁是皮谁是毛呢？按理说党应该是皮，党员，即使是领袖，也应该只是毛而已。但是在我的心目中，恐怕在所有中国人民的心目中，都不得不承认，毛泽东才是皮，中共不过是毛泽东这张皮上附着的毛而已。这恐怕也是中共至今不敢彻底否定毛的真正原因吧。当我在内心深处彻底地否定了毛以后，共产党在我的内心深处也就被彻底的否定了，不过这一切只能深藏于我内心的底处，直到我85年出国以后。出国以后我立刻就参加了海外民运，并正式登报退出了中共。后来在&lt;新闻自由导报&gt;上写了一篇揭露毛关于第三次世界大战准备牺牲四亿中国人的文章。不过那时网络还远没有如此发展壮大，新闻自由导报发行量有限，更可能因为人微言轻，另外可能是人心已经麻木，毛准备牺牲四亿中国人的惊人内幕竟然激不起一丝浪花，确实曾经令我悲伤。 <br />
<br />
毛泽东准备用牺牲四亿中国人口的代价换取世界大同，这是毛泽东1955年在中共内部讲话的确切数字，是我亲眼所见，可是这个数字我再也没有在其他地方见过，国内没有，海外也没有。到海外的这些年来，我偶尔看到有&lt;三亿&gt;这个数字，原来猜测可能是误传。最近看到马建介绍张戎新书&lt; &lt;毛，一个不为人知的故事》，知道三亿这个数字来源于苏共档案。这样看来，毛泽东在不同时期，不同场合，在不同背景下对第三次世界大战有过多次不同的论述。 <br />
<br />
解放战争期间许多共产党及其同路人对国共战争很害怕，害怕因此挑起第三次世界大战，毛泽东为了鼓动国共战争，曾对第三次世界大战有所论述，其主要思想是，第三次世界大战的危机虽然存在，但是在社会主义阵营的努力下是可以避免的。毛的这些思想在公开发行的毛选第四卷里有所论述，被认为是正面的，为中国人民广泛所知。 <br />
<br />
据马建介绍，张戎的毛传提到，毛在苏联说过U为了世界革命的胜利，我们中国准备死三亿人。可见国外所传&lt;三亿&gt;这个数字是从苏联流传出来的。毛泽东访问苏联是在1949年12月。当时中国人口的统计数字是4点5亿，三亿也正好也是三分之二，可见准备牺牲三分之二中国人，对毛泽东来说已经由来已久。倘若今天毛的徒子徒孙以同样的大手笔谋划下一次世界大战，也预计抛出三分之二中国人口，那可就是九亿啊！ <br />
<br />
如果说49年毛泽东在苏联对斯大林说准备用3亿中国人贡献世界革命，可能是想给斯大林送大礼，以博得斯大林丰厚回赠，那么1955年斯大林已经去世，抗美援朝又取得了“胜利” ，头脑发胀的毛泽东肯定是想用4亿中国人的生命为他个人获取大同世界领袖的头饺做祭品---我们很难想像他是打算为赫鲁晓夫做嫁衣裳。不过那时中国还没有原子弹，他的梦想只能借助苏联的原子弹往中国的土地上的投掷来实现。以毛的头脑，他不会没有预计到，一旦苏联投原子弹把美国的主力军队消灭了在中国土地上，美国一定会把所有的原子弹倾泻到苏联。美苏互投原子弹的结果，剩下的大同世界当然就是毛的一统天下了。 <br />
<br />
没有死的两亿中国人并不是毛泽东对自己的同胞还残存一丝仁慈的结果，是因为毛需要这两亿中国人去为他接收全世界。多么美好的梦想！又是多么愚蠢的梦想U苏联就会那么愚蠢地为他火中取栗？真想不到，一个把全世界都骗得团团转的超级巨骗，竟然也有把做梦娶媳妇当真事去办的时候。 <br />
<br />
后来赫鲁晓夫拒绝了毛泽东让其假做旁观，真投原子弹的蠢驴角色，毛泽东的梦想大受挫折，但依旧大梦不醒，转而要求赫鲁晓夫向中国卖造原子弹的技术，建造原子弹的工厂，从而开始了张戎的书中所说的，准备用饿死5千万中国人勒出来的粮食换取苏联的材料技术和设备来自己造原子弹。那末用肉体挡原子弹的人海战术是不是毛泽东当初批马寅初马尔塞斯人口论，推动中国人口大繁殖的初衷？十五年（1973年）超过美国的大跃进是不是毛泽东为发动第三次世界大战做物质准备？这些恐怕绝对不是空口无凭的猜想。 <br />
<br />
从马建的介绍来看，张戎的书中恐怕也没有涉及到我所看到的上述党内文献。这么多年我也没有看到报纸，杂志或网上有人提到过这些文献。这使我感到奇怪，因为这些文献应该会有更多的人看到，因为这本文献当时是在我们大学的内部图书馆发现的。所谓“内部”图书馆是供院级领导和马列主义教员专门使用的。我们学院是北京的一所普通大学，如果我们大学有内部图书馆的建制，有资格保存这样的文献，我相信北京的其他大学也应该会有。 <br />
<br />
我当时借阅了大约一个星期，可惜那时没有复印机，否则我一定会把相关讲话复印下来。不过由于震惊，记忆也就尤其深刻，几十年过去了，那些字句和数字依然深刻于我的脑海。我真希望本文读者的有心人中，能发动国内有资格进入大学内部图书馆的朋友重新找到这本书，将有关讲话复印下来，将其原文讲话公开于全世界。到那时，毛泽东也就不批自倒了，即使是毛的徒子徒孙也无言为其辩护了。我相信人类历史必将像抛马弃希特勒一样，永远地抛弃毛泽东。</div>

							
							
							
							
