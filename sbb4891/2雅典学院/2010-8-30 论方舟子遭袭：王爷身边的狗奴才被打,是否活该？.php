													<h2>继续转帖</h2>
						
						
																											<div id="postmessage_805171" class="t_msgfont">我们不同的诺懊运<br />
<br />
每个人都有些诺暗氖虑榛又不去，我们称之为命运。<br />
<br />
我跟方舟子，居然是一个专业的，说来你不信吧。我们都是学生物专业的，不同的是他先知先觉，本科毕业直接美国念博士去了。而我笨一些，居然还考了一个国内的研，再想出国。<br />
<br />
就是我想出国的这功夫，我发现了一个问题，一个很严重的问题。不，严格的来说，是两个问题。<br />
<br />
一个问题是，学术腐败，造假，还有学术无聊。从那个年代就开始了，具体的事例我不想说，不愿意说，但是行业内的人都心知肚明，而又无可奈何。中国不算超级有钱的国家，纳税人攒的这点钱，在被我们以所谓高科技的名义，陆陆续续都糟蹋掉了，而无任何实质上的科研成果，这让我很有犯罪感，我不能面对着含辛茹苦的纳税人说，看，小白鼠的这条基因上面的这个位点，被改变了以后，会导致这样一个蛋白质的产量多15%。纳税人会很疑惑的抬起头说，那又怎么样能？我会很严肃的回答说：不怎么样，这是科学，你不觉得很好玩吗？、<br />
<br />
基本上，也就是这样的所谓学术环境。无论中美，在无聊上都半斤八两。顶多是腐败和造假，是中国特色。<br />
<br />
那还有一个选择，就是从学校出来投身实业。很遗憾，我满目所见，基本上都是造价，诈骗和行贿。药厂行贿医生，从那个时候，94年就很流行了。而化妆品行业，补药，营养品，中药，也基本上是诈骗行业。其中最有名的是什么DNA营养因子。完全是毫无科学常识的胡说八道。居然每天能霸着中央电视台之类的主流媒体，轰炸人民的视觉听觉。<br />
<br />
两个选择都很罪恶。看来只有去美国，可是美国有美国的问题，存在一个“博士诱导骗局”的故事，这个以后有时间再讲。<br />
<br />
于是我改行了。<br />
<br />
你看，这个行业环境，居然这么王八蛋，让我改行了。当然，这个行业里，还有人做老师，很认真的做学问的，他们，都还是好人。可我又志不在教学。<br />
<br />
那个时候，满眼看到这些王八蛋的东西，其实也很想写点揭露黑幕的文章，但毕竟不容易在报纸上发表，又没有互联网，也就作罢了。<br />
<br />
我跑去做程序员去了，哈哈，君子爱财，取之有道嘛！干电脑就是光明，干净！哈哈，到今天我都如此认为。我爱钱，但我更爱干净的钱。<br />
<br />
方舟子就惨了。他不得不在生物里面转悠。这就意味着，他每天挥之不去的，都是这些丑恶，而他，没有选择我这样的逃避，也没有选择很多人一样的沉默，他选择了战斗。<br />
<br />
在这个领域，他有太多太多的材料，顺手拈来，即可打假！可谓是物产丰盛，不用耕耘，只管收获即可，保证来料取之不尽用之不竭！<br />
<br />
但是换一个角度想想，学术打假得罪个人，行业打假得罪企业。一个个知名教授，连同手下的弟子，饭碗和声誉，都靠骗了。一个个上市公司，毁灭起来简直易如反掌，可是这些企业养活了多少人的吃喝拉撒睡，都要靠骗人才能维持。你砸了人家的饭碗，一个不报复你，两个不报复你，你能保证所有的人都不报复你？<br />
<br />
这些事情，想想都可怕。方舟子在他百分之百的打假成功率背后，得罪了多少人，多得他自己都不知道。<br />
<br />
这些算是一点点内幕吧。这么多年来，我很难向别人解释清楚我为什么改行，说到良心，肯定会招很多人的耻笑。我不是说每个人都在骗人。但是有太多太多骗人的东西每天都在你眼前晃来晃去。你知道我的性格，我很难忍受的。<br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;良知与理性的血腥缠斗<br />
<br />
<br />
命运并没有告诉你，你做过ACT的牛人，你学过生物，你去过美国，你就必须要做方舟子，而且一做就做这么多年。<br />
<br />
但命运还会告诉你，方舟子就是方舟子，他是中国科技大学培养的无数生物学毕业身中，最杰出的一个。他是屈指可数，如果不是唯一一个，为科学和真理而献身的人。按照当年国家培养我们的时候给我们灌输的价值观念，以及到了美国以后被灌输的价值观念，方舟子是唯一一个甚至用自己的生命去捍卫去珍惜这个价值观念的一个。<br />
<br />
他以他学到的知识，无偿的告诉社会，不厌其烦的告诉社会，NO，这个不对，那个，也不对，而且他会一个一个的仔细证明给你看，不计后果的证明给你看。他摧毁了数以十亿计的诈骗产业，他让所有的骗子教授，骗子学者，睡不好觉。他是我们这个社会，唯一幸存的一只，全天候的啄木鸟，只要看到一颗病树，就会疯狂的扑上去一气猛啄，不把害虫抓出来誓不罢休，而且几乎百战百胜。<br />
<br />
可悲的一点在于，他的绝大部分打假工作，本来是属于行政机关的份内工作。但是这些行政机关，早已被收买，沆瀣一气与不法奸商勾结起来欺骗公众。而且这种情况非常非常之多，打不胜打。以方舟子嫉恶如仇的本性，他会不停的打下去，而且会越打越多。我几乎能够想象到他的黑名单，以几何级数的速度一再增长，不知何日是个尽头。<br />
<br />
方舟子的假，是打不完的，而他每一次打假，都会为自己树立起一组非常明确的仇人，因为利益受到侵害而形成的仇敌关系。<br />
<br />
我想，不是所有的仇敌都会用暴力来报复，否则方舟子活不到今天。但是，谁知道哪个仇人会来报复？又有谁能为他提供可靠的安全保证呢？他不是游侠，他是一个有家庭的普通人，也没有三头六臂，在一起蓄谋已久的报复面前，他几乎没有任何抵抗能力。<br />
<br />
是的，我们可以声援他。可是我们的声援在设计精巧的报复面前不堪一起，苍白无力。我们可以把报纸的头版毫无保留的送给他，可这么薄的一张纸，就真能够抵挡报复的毒箭？<br />
<br />
恩，我们可以很聪明的劝他，歇一阵子吧，躲躲坏人的风头。可是这难道不正是助长了坏人的嚣张气焰？<br />
<br />
面对这个造假成性的世界，我们是无解的，我们整日骂娘。<br />
<br />
面对这个造假成性的世界的唯一解，方舟子，我们仍然是无解的，我们在道义声援的背后，却是谁也不能保证他和他的家人永远安全。<br />
<br />
我不是方舟子，虽然我有足够的知识，但我控制使用，不给自己造成立即的危险。我也能逃避一个我看不惯的行业。我甚至可以在转瞬之间自杀掉我的所有ID，挂冠而去，顷刻之间消失的无影无踪。你少拿名气来勾引我，大爷不凛这一套。<br />
<br />
凡此种种，其实都是我们常人以求自保的东西。有这些，太正常了，没有这些，太不正常了。<br />
<br />
而方舟子，恰好就是没有这些的一个异数。<br />
<br />
我知道我没有资格为他写命运判决书，他并不是孔乙己，而我也根本不是鲁迅。想起方舟子，我的良心时刻受到谴责，为那些我未曾解开的罪恶；可与此同时，我又多么希望他能够有一个安全得到保证的生活。<br />
<br />
在方舟子面前，我的良知与我的理性，缠斗的鲜血淋漓。<br />
<br />
那，你呢？</div>

							
							
							
							
