																											<div id="postmessage_804750" class="t_msgfont">方舟子遭袭，遭袭，千真万确。当然不是我说的，是他老婆在微薄上且以“两袖清风、铮铮铁骨、无怨无悔”向天下的写手、文人、妓者、婊子、还有鸡鸣狗盗之徒宣布的，当然宣布的对象也包括被共党耍的屁民。不宣布还好，方舟子老婆这样一捣鼓顷刻让天下的所谓的热爱“言论自由”者胆寒了，虽然在方舟子没被所谓遭袭前，他们自己也老在舔着当权者的股沟，清理权贵的屁眼，时时刻刻按照他们亲爱的党的政策亦步亦趋，可是此刻却突然记得“言论自由”这个物件原来就是代表自己可以尽情的给党当传声筒，做好喉舌，为党的利益，为党中某些人的利益摇旗呐喊，反这反那。但是这次袭击这样的事例让他们看到，做好党的喉舌，写写软文，说点浑话，惹恼了谁，一不留神也会是要吃上锤子的。所以，那些文人、妓者、写手、砖手、党的打手，有点鸡动。<br />
<br />
&nbsp; &nbsp; 我很奇怪，为什么那两人要带个锤子，带上辣椒水去行刺？为什么不一人带镰刀，另一人带斧头，用镰刀斧头帮的架势，把方舟子活活吓死，省得背上伤害“历史伟人”的罪恶。当然，我是知道的，方舟子同志政治正确，镰刀斧头帮自然是不会拿他千刀万剐的。所以，带个锤子，带着辣椒水这样的凶器可以看出，行刺的这两人水准实在是没法跟台上的流氓、土匪相提并论的。<br />
<br />
&nbsp; &nbsp; 缘由于此，现在大行其道的提法，诸如把这两人唤为“歹徒”，再将他们打上威胁“言论自由”的标签，加害“中国良心”的帽子。我觉得是不妥的。我上文提过，台下的你再流氓你流氓的过镰刀斧头帮？你再土匪土匪的过伟大光荣的一贯道？绝无可能吧。因为事实胜于雄辩，最歹毒的货色业已存在，何来还有把至今身份不明的袭击者，现行扣上“歹徒”这样的字眼，把他们归并入镰刀斧头帮的行列呢？这不是侮辱了，赐予你们饭吃，给你们话说，给你们满嘴跑火车的权利的一贯道的伟大嘛。<br />
<br />
&nbsp; &nbsp; 再一个，天下人都纷纷把这次所谓幕后的黑手臆测为方舟子同志的所谓仇家，那些被其威风的狼牙棒砸到的人马。我倒觉得未必，因为一贯道的警方尚未查出于此关联的信息，这样的臆测简直也是好笑，当然就像汪精卫刺杀亲王时，亲王的奴才们也可臆测是亲王踩到了哪个具体的人尾巴，而实行了报复，但是事实是汪精卫想以此震慑下清廷，为共和埋下一个伏笔。所以，我说，臆测作为小说可以流传，但是作为佐证方舟子遭到的复仇的，更衍生出其“两袖清风”的逻辑，我想这是要搞出新时代的杨乃武与小白菜的冤案的。<br />
<br />
&nbsp; &nbsp; 我是这样认为的，为什么那两人是不能被方舟子舔一贯道的股沟惹火，清理其的屁眼而感到恶心，而想把其人从地球上抹去，向世人证明任何与一贯道眉来眼去，水乳交融之徒，虽无权利持镰刀斧头砍杀，但是借把锤子也要吓死鸡鸣狗盗的货色呢？为什么他们不能是看不惯政治正确的嘴脸，在一贯道的环抱里飞扬跋扈的无耻文人，无耻作家、无耻“院士”的表演，想借此表达下亲共者虽远必诛的豪情呢？<br />
<br />
&nbsp; &nbsp; 如果，真相正如我所言，他们还是歹人吗？还是恶棍吗？如若真是如此，那么这两人应该就是义士，就是勇士。那些惶恐于所谓言论自由被黑道遏制，而献媚于红道者，寄望一贯道来主持言论自由的章法，寄望奉天承运是不是觉得有点滑稽跟无耻呢？诚然，这也是我的臆测，但是献媚于朝之徒混混国中，与君上亦步亦趋之徒成为斗士的国度，如杨佳般义士的出现，也算不得是什么新闻了吧。<br />
<br />
