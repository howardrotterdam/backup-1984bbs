													<h2>北京厨子:我所知道的方舟子</h2>
						
						
																											<div id="postmessage_805169" class="t_msgfont">不招人喜欢的牛人<br />
<br />
方舟子是我们的老相识了。<br />
<br />
12年前，很遥远的时代，那时候，四通论坛的几个网友，闲极无聊的，非要去拍方舟子。顺便说一句，在网上，我们那个时代，拍有可能是表达亲切感情的一种方式。拍是拍砖的拍，多少网友就是在砖头拍来拍去中拍成了莫逆之交。正所谓“不打不相识”。<br />
<br />
遗憾的是，论坛一众老流氓，失望而归。原因很简单，方舟子气量狭小，毫无幽默感可言。嘁哩喀喳封掉几个网友的IP，而且很义正词严的批评了以XXX，YYY，ZZZ为首的流氓无赖集团。<br />
<br />
我们在网上互称流氓是诙谐戏谑，但他老人家说谁是流氓，那可是文革口吻。<br />
<br />
大家讨了无趣，自然四下而散了。<br />
<br />
方舟子因此而丢掉了跟很多人做朋友的机缘。<br />
<br />
我没有介入这场挑逗之中，因为我从一开始就发现他特别无趣。<br />
<br />
但不影响，方舟子是比我们老一代的流氓。说到老流氓，是我们那个时代对网络大腕的敬称。今天的五岳散人，可以列入老流氓行列，也就是说，新浪粉丝不上十万，赶紧滚蛋。<br />
<br />
我们这一代，是1998年的网络流氓。他那一代，居然要到1994年。<br />
<br />
别一看1994年就觉得遥远，老掉牙。今天我们所有的网络游戏，也不过就是当年的翻版而已，别以为你80后，90后多牛逼。<br />
<br />
1994年，那时候没有网页。恩，当然没有新浪啊，及新浪的前身四通利方。那时候中国还没有互联网。但是美国有！<br />
<br />
美国的这个互联网是以邮件列表方式来玩的，全世界的人，按照话题组，没事相互发邮件玩。其中一个话题组，叫中国话题组，英文缩写是ACT。<br />
<br />
ACT八大牛人，个个顶呱呱，方是其中之一。这个，方舟子有专门的介绍。我看过其中的一个，排名第一第二的，叫图雅，人家写的那个东西，真牛比，放到今天也是韩寒级的。<br />
<br />
我想这几个网友是想以拍砖头的方式去接近方舟子，都是牛人，看上你了，直接巴结求签名去你妈的谁都不干，但打几组友谊拳，因此结识，岂不雅致而有趣么？<br />
<br />
但方舟子就是这么无趣。这个无趣的性格，将伴随他终生，让他丢失太多太多的朋友。<br />
<br />
其中好多朋友本来是可以为他两肋插刀的。但是他认为毫无必要。在这么个险恶的社会里，他居然会认为两肋插刀的朋友很俗。<br />
<br />
他就是这么个人。牛，但是，不招人喜欢。<br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;他不姓孔他姓方但他不是孔方兄<br />
<br />
这是1998年或者1999年的故事了，时间很久，不一定记得非常清楚。<br />
<br />
那时候我们几个四通利方论坛的老流氓，终日里到处流窜，孤独求败，到处惹是生非，实在是太寂寞了，三天不打架痒得难受。那时候，互联网，就是一个闲的蛋疼的人玩的东西。花钱（那时候拨号上网一个月能花一千多，那时候很多人工资才两千多），花时间（别跟我扯什么围脖控，比我们那个时代差远了），而且没收益，没有任何可以梦想的收益（看着今天有人敢骂那些想花钱帮着围脖做推广的，简直是恍若隔世）。<br />
<br />
寂寞归寂寞，折腾归折腾，一个很现实的问题摆在面前：未来怎么办？<br />
<br />
想到这里，我不禁对方舟子同学有些感叹。虽然他极其无趣，但是这家伙居然从94年就开始在网上晃，晃了这么多年，居然没饿死？真的没饿死？还有心思给别人写什么《ACT时代八大牛人》？靠！丫的真是闲的慌。<br />
<br />
大概到了1999年，四通利方开始商业化了，商业化以后起来个新名，叫新浪。<br />
<br />
四通利方商业化了，可是我们没有。我们一帮老流氓，得挣个吃喝钱吧？于是大家纷纷就自己的前途作了选择题，学文的，转行去了报纸写专栏，当时就这么一批人出去当媒体人了。大概什么李承鹏，类似经历（印象模糊，不详）。像我这类的非文字类的，该干嘛干嘛去了，我就去美国写程序去了。<br />
<br />
话说2003年的时候，大家非常吃惊的发现，方舟子这个家伙，居然还在网上晃！弄什么新语丝，弄个什么打假。大概那年，他打了一个基因皇后的假。<br />
<br />
从这个时候起，我对方舟子的看法，从原来的不牛慢慢的有了一些尊敬。毕竟都是过来人，知道坚守，是个多么辛苦的差事。<br />
<br />
再往后，大家更是各奔东西了，论坛时代彻底衰竭了，所谓“著名网友”，大概除了脱衣服的流氓燕，越来越少了，有，也是奔着别的路子发展去了。<br />
<br />
依稀在大家伙各奔“钱”程的生活中，偶尔，还会在网上，电视上，看到方舟子那毫无取胜希望的打假。而且我们知道，他不是那种王八蛋，左手打假，右手勒索的事情他从来不做，从来是抓住一家往死里打，给他多少钱都没用。<br />
<br />
偶尔我会想，这孙子，这么玩，吃什么，喝什么啊？名气这东西能当饭吃啊？<br />
<br />
也就是说，偶尔，我会想起孔乙己，这么一个人物来。</div>

							
							
							
							
