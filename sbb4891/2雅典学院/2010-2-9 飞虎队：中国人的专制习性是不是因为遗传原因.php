													<h2>飞虎队：中国人的专制习性是不是因为遗传原因</h2>
						
						
																											<div id="postmessage_473595" class="t_msgfont">中国人的专制习性是不是因为遗传原因<br />
 <br />
<br />
&nbsp; &nbsp;一百多年来，中国人一直都在，或者说至少是自认为自已一直都在孜孜不倦地追求着民主的梦想，但是每每事与愿违，事到临头来，在具体的社会生活的方方面面中总像着了魔似的顽固地重复于专制的行为模式中，与世界的民主大潮背离得反而越来越远。这种情形像是患上了某种强迫症。中国人这种根深蒂固的专制习性显然已经无法用常理来解释了，不得不促使我们从其他方面来加以考虑。 <br />
<br />
&nbsp; &nbsp;当我们说到“中国人的根深蒂固的专制习性”的时候，显然这个专制习性是属于一种性格上的因素，而“根深蒂固”这个形容词，虽然只是一种比喻，但是却非常恰当地，非常形象生动地表明了中国人的这种性格与其先天性因素的某种联系。 <br />
<br />
&nbsp; &nbsp;那么我们需要搞清楚的就是：到底中国人的这种倾向于专制的性格跟遗传因素有没有关系，而进一步我们要知道，作为一种整体性的社会行为（对专制的倾向性）跟中国人的遗传因素有多大程度的关系，而自然选择对中国人的整体的专制习性的形成起到了多大程度上的作用。 <br />
<br />
&nbsp; &nbsp;关于性格是不是受遗传因素或者说基因的影响的问题，科学家已经有了明确的研究结果。答案是肯定的。 <br />
<br />
&nbsp; &nbsp;这方面的研究很多，比如：美国明尼苏答大学通过对数千对同卵孪生子的研究表明，遗传基因对人的性格特征的形成的影响程度一般可达到将近百分之五十的比率，这称为遗传率。因为同卵孪生子在基因方面是完全相同的（类似于克隆人），通过对比他们在后天环境中长大以后的性格相似性，就可以得出这一结论。 <br />
<br />
&nbsp; &nbsp;实际上，从我们日常生活中也可以观察到类似的现象，很多还在婴儿期的小孩，有些表现出极强的攻击性，有些则很文静，说明人的性格具有很强的先天性。 <br />
<br />
&nbsp; &nbsp;但是遗传因素对性格的影响过程是很复杂的，虽然科学家得出了一个大致的影响程度的比率，但是仍然很难推断出具体哪些性格特征是受遗传因素影响而产生的，是如何产生的，而且也很难说人的性格是由某一两个基因决定的，一般认为是多种基因相互作用的结果。 <br />
<br />
&nbsp; &nbsp;而且，中国人的那种对专制的倾向性，与其说是一种直接的性格特征，不如说是受到性格因素影响而决定的一种社会行为模式。那么，从这个角度来说，有一种更好的理论能够解释它的产生。 <br />
<br />
&nbsp; &nbsp;哈佛大学的生物学家E.O.威尔逊，在七十年代及八十年代出版了《社会生物学：新的综合》，《论人性》，《基因，心灵与文化》等著作，创立了一门研究动物及人的社会行为模式与遗传因素及自然选择的关系的新学科--社会生物学。他把达尔文的进化论关于生物的生理特征受自然选择影响的理论更加扩展了一步，研究得出了人的性格及社会行为模式也是主要由遗传基因及自然选择的影响而决定的结果，这一结果现在已经普遍被学术界接受。 <br />
<br />
&nbsp; &nbsp;按照威尔逊的理论，由于自然选择的作用，不仅仅在生理方面，而且在社会行为模式，社会心理模式等方面，如果一个个体具有有利于其生存和繁殖的某种特征，那么，他就会将这种特征通过其遗传基因一代代传播扩散下去。而那些具有不利于其生存繁殖的某种特征的个体，则会逐渐被淘汰，并导致这种特征在这个群体中逐渐消失。 <br />
<br />
&nbsp; &nbsp;而某个人类群体所具有的某种特有的社会行为模式，社会心理模式，乃至一个民族的文化特征，都可以说在很大程度上甚至主要是受其遗传基因影响而决定的。是在其长期的特有的生存环境中自然选择的结果。 <br />
<br />
&nbsp; &nbsp;科学家通过研究已经将这种推断接近到了甚至某种社会行为模式有可能仅仅只是由几种基因而决定的程度，而且预测将来这些基因都会被鉴别出来，从而将性格，情感，智力等等心理上的特征以可量化的方式与基因的作用联系起来。 <br />
<br />
&nbsp; &nbsp;那么根据这一理论，某个人类群体所固有的社会行为模式主要是由其遗传基因所决定的。而这一结论，根据前面对同卵孪生子的研究结果也是说得通的，虽然其研究表明，人的性格受遗传基因的影响程度只有不到百分之五十，另外一半是受后天环境影响而产生的。但是遗传因素和后天环境因素的影响是交互作用的，一个人，虽然其性格只有百分之五十受遗传因素影响产生，但是在后天环境中成长时，这百分之五十的先天遗传因素不仅决定了他性格中一半的成分，而且会导致他主动地选择后天环境对他的影响，导致他主动地去选择他喜欢接触的信息，主动地去选择符合他先天性格成分的事物的影响。也就是说，先天遗传因素在与后天环境因素的较量中占有先发优势。 <br />
<br />
&nbsp; &nbsp;结论就是，最终形成的某个个体乃至某个群体的某种特有的社会行为模式主要是由其遗传基因而决定的。 <br />
<br />
&nbsp; &nbsp;我们可以通过对网络上的中国人群体对民主专制等问题的反应来说明这一点。虽然人们往往把这种反应归因于愚民教育和信息封锁所产生的影响。但是这是经不起推敲的，因为，第一，至少对于海外的中国人来说，这种影响是不存在的，但是他们大多数人仍然本能地倾向于专制。第二，对于海外和国内的两种中国人群体分别来说，他们对于各自所面对的信息环境和获得信息的条件和机会是同等的，但是人们仍然是本能地选择他们所希望接触所喜爱接触的信息：大多数人选择倾向于专制的信息，少数人选择倾向于民主的信息。 <br />
<br />
&nbsp; &nbsp;虽然并没有准确的统计数字，但是这一点可以根据中国人对于西方民主国家的态度大致推断出来。我们姑且粗略地估计为：三分之二的中国人倾向于专制，三分之一的中国人倾向于民主。 <br />
<br />
&nbsp; &nbsp;那么接下来，我们要知道的是，到底中国人特有的生存环境是不是具有某种对于中国人特有的专制习性的形成起到了特殊作用的力量。因为我们知道，现存的人类各种族都是起源于二十万年前的非洲智人，应该说在种族上并没有本质的差别。并且直到几千年前的氏族部落时代，世界上每个种族和民族都经历了一个军事民主制的阶段。那么是什么力量的作用使得中国人不可逆转地进化成了一种具有压倒性的专制性的社会行为模式。 <br />
<br />
&nbsp; &nbsp;当然最直接的解释就是自然选择的作用，而且这也是最普遍的原因。但是仅仅以自然选择为由显然不能完全地解释这一结果。虽然中国地区特殊的地理环境导致了中国人生存环境的极端封闭性（仅次于美洲印地安人的生存环境），使得自然选择作用的结果尤为严酷。 <br />
<br />
&nbsp; &nbsp;更重要的是在中国人的社会生存环境中还有一种极强的人工选择作用，那就是中国特有的基因清洗方式--族刑和连坐。 <br />
<br />
&nbsp; &nbsp;中国古代的统治者一开始发明出这种制度化，系统化的种群清洗方式，就带有很强的“斩草除根”的意识，而不仅仅只是为了报复泄恨，不然他们不会那么早就将其全面地制度化，系统化。 <br />
<br />
&nbsp; &nbsp;连坐最早在商代就已经出现（同时出现的还有一种人殉的制度，当然这是奴隶社会都共有的一种现象）。然后到了秦朝时已经将连坐和族刑制度化，族刑并且从诛三族发展到了诛七族，到了隋朝时，已发展到了诛九族。最后到元明清三代时发展到了登峰造极的地步，特别是明清两代，因言获罪的比比皆是，受连坐，被族诛的动辄数百人乃至上万，单是蓝玉一案，受牵连而死的就达数万人。 <br />
<br />
&nbsp; &nbsp;中国古代这种极端野蛮残忍的逆向淘汰方式，可以说将中国人中稍有反抗精神，革新精神的个体都已经清洗殆尽，将中国人中的优良基因淘汰殆尽。 <br />
<br />
&nbsp; &nbsp;因为这种手段不仅仅只是将具有反抗精神和革新精神的单个个体从肉体上消灭，还连带将具有这种基因成分的其他有亲缘关系的整个族群都一并灭绝了。中国历史上很多优秀的个体和群体都是这样被有计划地，系统地灭绝了。 <br />
<br />
&nbsp; &nbsp;一直到了近代，中国社会中仍然残留着这种野蛮的习俗，国民党和共产党，军阀，地主豪强，等各种政治势力，在彼此的政治斗争中，都延续着对对方“斩草除根”的习惯。一直到了六七十年代文革时期，中国社会仍然存在着大量的社会个体因为受亲人朋友的政治牵连而被剥夺社会权利的案例，并严重影响到了其生存和繁殖的过程。 <br />
<br />
&nbsp; &nbsp;我不是研究法制史的，不太清楚其他国家和民族是不是也有类似的制度，据我所知，似乎古埃及有过类似的做法。但是象中国这样将这种逆向基因清洗方式在自己本族内制度化系统化地全面实施了几千年的恐怕绝无仅有。 <br />
<br />
&nbsp; &nbsp;很明显的，在中国社会中，具有倾向于民主的社会行为模式的遗传基因的个体，是不利于自身的生存和繁殖的，最终会被逐渐淘汰掉；而具有倾向于专制的社会行为模式的遗传基因的个体，是有利于自身的生存和繁殖的，其遗传基因最终会通过繁殖一代代扩散下去，最后具有压倒性的优势。这就是造成中国人那种根深蒂固的专制习性的根本原因。 <br />
<br />
&nbsp; &nbsp;读者可能会问：既然如此，具有倾向于专制的社会行为模式的遗传基因，是有利于自身的生存和繁殖的，那么它就应该是一种优良的基因；而具有倾向于民主的社会行为模式的遗传基因，是不利于自身的生存和繁殖的，那么它就是一种劣势的基因。既然自然选择已经造成了这种结果，那么它就是合理的。 <br />
<br />
&nbsp; &nbsp;我想，从表面上来看，是很难区分哪一种基因的优劣，但是，倾向于专制的社会行为模式的基因，它所造成的不仅仅只是专制的社会行为模式这一种结果，而且还连带导致了个体和社会群体在道德品质上的全面低下。并且，倾向专制的社会行为模式，形成的是一种低质量的生活方式。这已经被历史和现实所证明了。 <br />
<br />
&nbsp; &nbsp;实际上，我在中国这个国家所犯下的最大的错误，就是曾经天真地以为：这个社会中大多数人的品质是好的。但实际上，从现实生活中得到的教训是恰恰相反，只有少数人的品质可能是好的，而大多数人的品质很难说是好的。 <br />
<br />
&nbsp; &nbsp;我想我可以把中国人这种专制性格分解为两部分，一部分是对于专制力量--强权的无条件服从习性，另一部分是对于弱势者，异己者的毫不宽容的专制习性，实际上，大多数中国人的性格可以用一句话来概括：那就是“欺软怕硬”。 <br />
<br />
&nbsp; &nbsp;那么我们是不是又可以认为，既然中国人已经基本被定型为这种倾向于专制的社会行为模式（既是自然选择的结果也是人工选择的结果），那么就应该顺其自然，“尊重”大多数人的选择？对于追求民主的人说：从此死了这条心。 <br />
<br />
&nbsp; &nbsp;我想我没有理由这样认为，因为如果我们仍然生活在一百多年前，我们确实是仍然可以在给皇帝和官老爷磕头请安，服劳役，挨板子，白天耕地给老爷缴租税，晚上回去点起油灯打老婆这样的生活中继续麻木地过下去。但是现在不同了，即使大多数的中国人因为遗传基因的作用，本能地仍然倾向于专制的社会行为模式，但是，至少在思想上，受外界多多少少的影响，仍然产生出了蒙昧的对于民主的生活方式的要求。而且更重要的是，专制统治，给绝大多数中国人确实是造成了现实的巨大痛苦。 <br />
<br />
<br />
而且我们更没有权力忽视至少还有三分之一的中国人是可能残存有倾向于民主的社会行为模式的基因的，是希望过民主的生活的。 <br />
<br />
&nbsp; &nbsp;但是不幸的是，从上面论述可知，倾向于专制的社会行为模式的基因在中国人中占据了具有压倒性的优势，所以不可能指望民主的种子会自发地从中国社会内部产生出来，这已经被历史无可辩驳地证明了。 <br />
<br />
&nbsp; &nbsp;所以，中国社会要想实现民主的转变，只能是依靠外力作用，只能是从文化的根基上加以改变，和平演变也好，全盘西化也好，文字拉丁化也好，引进基督教价值观也好，都不妨去努力推动，一直到扭转中国人的逆向的基因演变趋势。 <br />
<br />
&nbsp; &nbsp;不仅仅是中国人，实际上除了西方，整个世界其他国家民族都不可能自发地产生出民主的萌芽，只能依靠外力的作用产生。 <br />
<br />
 <br />
<br />
&nbsp; &nbsp;这一点，我们看看伊拉克的情况就很容易明白了，英美法等西方国家在几百年前就能够自发地产生出自由民主的萌芽并最终成形。而亚非各国即使到了信息时代的今天，仍然要靠着外部力量输入民主才能摆脱专制独裁的统治，甚至都不一定成功。 <br />
<br />
&nbsp; &nbsp;但是伊拉克人民至少还有这样的觉悟，懂得对萨达姆的专制独裁统治采取不合作态度，美军来了就扔掉武器投降，除了少数死硬分子，大多数人仍然明智地选择了跟占领当局合作的态度。难道我们中国人的觉悟还不如他们吗？ <br />
<br />
&nbsp; &nbsp;我想我们中国人要是想有所改变，可能是应该学学伊拉克人了，是要具有一种准备着给美军带路的精神才行啊。 <br />
<br />
&nbsp; &nbsp;试想，如果没有鸦片战争，我们现在肯定还在上述那种“给皇帝和官老爷磕头请安，服劳役，挨板子，白天耕地给老爷缴租税，晚上回去点起油灯打老婆”的生活中麻木地行尸走肉一样地活着，这是毫无疑问的事情。 <br />
<br />
&nbsp; &nbsp;中国人不仅无法自发地产生出民主，而且也搞不好别人输入的民主，中国人建立的另外一些国家，如台湾，新加坡，都是典型的例子，新加坡甚至到现在都还是一个世袭的专制王朝。 <br />
<br />
&nbsp; &nbsp;实际上，我认为，中国人就是管不好中国人自己的事情，专制也好，民主也好，中国人倾向于专制的社会行为模式的那种遗传基因决定了中国人的基本性格就是“欺软怕硬”，决定了中国人永远都是压迫残害中国人自己本族人的最凶狠的人，永远都是专制强权最可靠的帮凶。所以我认为，未来中国的民主政府，完全没有必要选举任用中国人自己来管理，完全可以直接聘请西方人来管理。 <br />
<br />
&nbsp; &nbsp;关于这一点，香港就是一个最好的证明，也是中国最好的榜样。香港一百年来的历史无可辩驳地证明了：中国，作为西方管辖地而永久地存在下去，是确保人民幸福的最可靠的政治依托。 <br />
<br />
&nbsp; &nbsp;作为国家的中国这个概念，一直都是生活在这片土地上的人们最严酷的一种精神枷锁，一直都是统治者用来压迫残害中国人的最有力的一个思想工具，一直都是中国人杀戮奴役中国人最好的一个借口。它的影子一直都像恶梦一样缠绕着我们的灵魂。 <br />
<br />
&nbsp; &nbsp;我认为，作为国家的中国这个概念，她根本就没有存在的必要，她作为文化上的概念而存在就已经足够了。 <br />
<br />
&nbsp; &nbsp;我是很想知道，中国人，特别是中国人中以知识分子自诩的那些人，他们，到底有没有这样一种智慧，有没有这样一种勇气，来跟作为国家的“中国”这个概念作一个彻底的决裂。</div>

							
							
							
							
