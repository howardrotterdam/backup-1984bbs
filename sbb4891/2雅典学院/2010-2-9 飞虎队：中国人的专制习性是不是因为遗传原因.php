另外，新罗国的王族姓金，也就是说至少从那个时候开始就有姓氏了，考虑新罗国的年代，这时间不短了吧？<br />
另外，我刚找了下资料，以下是粘贴。包括同样的姓氏所包含的不同的血统，以及同一血统包含不同的姓氏（这说明血统的分散度在朝鲜很高），我认为应该可以回应你关于融合时间长短以及其他方面的质疑了，同时也能证明我的观点。<br />
<br />
<br />
<br />
基本上朝鲜民族公认的始祖是在商(中国的朝代)亡后,由商朝移民迁移过去的,即朝鲜所说的&quot;s子朝鲜&quot; <br />
他们同当地(朝鲜半岛)的人通婚后产生的后代就是朝鲜民族,而在很长一段时间他们保留了先民无姓氏的传统(一如日本民族及中国上古时平民无姓氏)----除了掌握权力的贵族. <br />
这金,李在朝鲜属于贵族姓氏,推断应是朝鲜在臣附于中国王朝时,由中国皇帝所恩赐的姓. <br />
至于平民百姓的姓名大概是从唐(中国的朝代)以后,中朝之间的往来(遣唐使),及儒家文化的传播才开始发展起来的. <br />
基本上和中国人无关(即:并非是中国的某姓氏族迁移的朝鲜而形成的) <br />
研究朝鲜姓氏要了解东北地区的民族构成,通古思民族语言大量山河林姓氏等音译成汉语读音很具代表,比如中国满族的老姓汉译音成为了满洲族东北人群同化的代表.朝鲜民族在远古时用名不用姓,这在人类历史上很普遍。 <br />
中国的使用姓的制度无疑在行事居民管理上有很多优点，所以古朝鲜从中国引进了使用姓的社会管理方式。 <br />
开始时只有皇家与上层贵族，官吏给姓的称号,后来由上至下,劳工,平民也使用姓。很多姓与自己的主人相同。 <br />
所以朝鲜人多同姓， 同时当时的姓是一个是地位的象征， 同时借用了很多汉姓， 所以与汉人虽然同姓， 但毫无血缘关联。 <br />
在姓氏制度形成后， 姓与血统的延续的关系才建立起来。 <br />
在韩国有如此众多的姓金， 就是上述原因， 金是比较流行的姓， 而众多的金完全不是同宗的 <br />
金姓有几个来源，第一来自新罗国王金阏智，第二来自伽耶国王金首露。朴姓来自新罗国王始祖朴赫居世（新罗国王虽然是同一家族，却有朴、金两个姓）。这两个姓都属于来自神话传说的姓氏。济州岛的高、夫、梁三姓也来自神话传说。百济国王扶余氏和高句丽国王高氏是自己起姓。 <br />
<br />
还有一些姓氏是古代中国皇帝或者韩半岛三国国王赐姓（比如新罗国王赐的金海许氏、中国皇帝赐的甘泉文氏）、自己创氏改姓（高丽灭亡后，王氏王族纷纷改姓玉、全、田等）。百济义慈王之子扶余隆在亡国后去中国唐朝学习，被唐高宗赐姓曰徐。新罗第三代儒理王在将全国划分为6个部落后，给每个部落的酋长都赐姓，比如俱礼马赐姓为孙，苏伐都利赐姓为崔等等。 <br />
<br />
还有国王给“乱民”起侮辱性的姓，比如高丽太祖给百济遗民起的牛、马、象（后改为尚）、獐（后改为张）、豚（后改为顿）等姓氏。还有一些来源不详的古代姓氏，比如从箕子朝鲜传下来的鲜于、徐，辰韩传下来的苏姓，百济时的“八大姓”――沙、燕、解、国、真、苗等句丽国王高氏是自己起姓。<br />
