																											<div id="postmessage_420119" class="t_msgfont">乌鲁木齐大屠杀：中共的阴谋 <br />
<br />
作者：犀烛 <br />
<br />
也许有汉族人或少数民族人有错觉，就是在处理民族问题上中共只是偏袒了某一方的问题。其实，从乌鲁木齐屠杀中可以看出：挑动民族仇恨是中共的一贯伎俩。中共玩的就是既亲自动手制造了民族分裂和仇恨，又摆出一付“打击民族分裂”的道貌岸然面孔的招数。 <br />
<br />
关于新疆人强奸汉人女子的谣言，之所以能在网上迅速传播，没有五毛的煽动，能造成这么大声势吗？所以，谣言与其说是汉人放的，不如说是中共放的。 <br />
<br />
仇恨激起来了，那么下一步安排的殴打新疆人的戏就可以说成是因仇恨而引发的汉维械斗。中共的策划、组织、指挥罪恶就被掩盖的一干二净，甚至还可以高喊“严惩凶手”。同时，中共又利用关于“械斗”的处理和舆论引导，掀起了第二波更大的煽动仇恨的浪潮。让每个民族的人都觉的自己很受压抑，受到了委屈。维族人觉的自己被抹黑、被打了，还没处说理。汉族人觉的政府的处理太软弱，不去惩办“强奸犯”，反而惩办“凶手”，愤愤不平。 <br />
<br />
有了这些铺垫，接下来的乌鲁木齐大屠杀中共就好办多了。其实，在一贯高度戒备的新疆、在省会城市，要组织一次万人游行绝非易事，中共肯定早就得到了线报。它为什么不事发前阻止呢？为什么要开枪呢？就是它要制造这么一场新疆人因民族仇恨制造“暴乱”的假象。什么能促使新疆人上街？韶关“冲突”。而新疆人一上街就可以指为“暴徒”，大打出手。因为有韶关“械斗”的舆论引导在前，人们很容易联想到乌鲁木齐动荡是不是因为新疆人出于仇恨，行为过激啊？如果没有韶关这场戏，那死伤这么多人中共是很难逃脱罪责的。同时，中共可以进一步狡辩乌鲁木齐死人只不过是韶关翻版而已，是群众械斗的结果，中供派兵不是杀人，而是“维持秩序”。由于中共已在汉族群众心中播下了仇恨，那么新疆杀人，许多汉族群众就不会谴责中共，而是觉的杀的好，杀的解气，早就应该这么强硬。甚至仇恨的火更大――维族人竟然这么无法无天，打、砸、抢到省会来了，不好好治治还得了！其实中共每次屠杀（包括镇压法轮功）都是这样操纵舆论的，让人们不是去谴责屠杀者，而是对被屠杀者充满仇恨。 <br />
<br />
可见，乌鲁木齐大屠杀完全是一场精心策划的阴谋，从事态发展来看，向符合中共需要的方向环环相扣发展，这是偶然事件能做到的吗？而且，操纵舆论相当精致，也就是说，网络上“汉人”的声音未必就是汉人发出的，其实“新疆人”的声音也未必就是新疆人发出的。通过舆论引导，汪洋成功的塑造了“最大受害者”、“反对民族分裂”的正面形象。如果不引导一下，在汪洋地盘上出现了这样一场阴谋，如同在贾庆林手里出现“蓝极速”纵火一样，能不让人议论纷纷么？ <br />
<br />
当然，可以猜测最后的千夫所指如同拉萨屠杀一样一定是政法系，本来舆论引导就是这么暗示的――要同情胡系，要支持胡系扳倒江鬼政法系，江胡权斗就是希望，就是一切。政法系本身确实恶贯满盈。不过如果真是汪洋在不知情情况下被别人搞了，那汪洋为了自己的利益，对韶关那些处置不力，甚至有意纵容，从而让他丢脸的官员一定会狠狠收拾。如果还出现韶关维族人手机被没收，没有说理机会的现象，有点费解。就如同周永康到处大打出手，按理说严重败坏了某人的声誉，某人却始终制止不了，只有频频发消息撇请关系一样费解。 <br />
<br />
政法系要干的就是这种制造冲突、动荡和高压的事，这样它们的权力才能越来越大，所以可以说这件事绝对是政法系干的。但非常大的事，其实如果没有其他派系点头、默许，政法系的阴谋也只能是无疾而终。所以，在这件事上，整个中供全都罪责难逃。 <br />
<br />
乌鲁木齐屠杀中共迷惑了很多人，包括外国政府，死伤这么多人，只是表示“遗憾”。因为他们听信了中共精心炮制出来的谎言，以为人真的不是中共杀的，真的是民族冲突造成的悲剧，是骚乱的结果。中共下一步就是将石首屠杀经验推广到全新疆乃至全国，用高压的鲜血来保证流氓的权力。这是中共必然要干的，而我们要做的就是揭穿中共，发出强有力的谴责之声，遏止中共末路的邪恶。杀人不是中共的什么重振旗鼓，而是回光返照，是无能的表现，只要我们努力遏止，抗议不息，邪恶嚣张不了多长时间。相反，如果对中共杀人听之任之，让中共频频得手，甚至从此“吸取教训”，龟不出头，中共看到不会赔本，果然有效，免不了会越杀越凶。不是说这样中共就能避免灭亡，只是说会给百姓带来较大损失吧。 <br />
