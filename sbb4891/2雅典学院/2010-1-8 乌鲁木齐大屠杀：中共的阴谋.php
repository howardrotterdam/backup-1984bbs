													<h2>中共政治2大怪现象和新疆75惨案中的5大疑点</h2>
						
						
																											<div id="postmessage_420122" class="t_msgfont">中共政治2大怪现象和新疆75惨案中的5大疑点&nbsp;&nbsp;<br />
――新疆75事件中的疑点 横河分析 <br />
打印版 【 阿波罗新闻网2009-07-13讯】&nbsp;&nbsp;<br />
<br />
<br />
各位听众大家好，我是横河。今天和大家一起讨论一下新疆事件当中的一些疑点。大家知道新疆事件发生，和前面6月25日韶关发生的事件是紧密相关的。韶关事件发生以后，按照中共长期的新疆政策，就是防范新疆独立和防范所谓新疆的恐怖主义活动，这个政策我们可以从中共的一些文件，和主要领导的讲话当中都可以看出来。在奥运之前，我们知道奥运有一个安保，它设了5大势力作为奥运安保的主要对象，这5大势力里面，就包括了所谓的疆独。奥运以后，中央政法委提出来，以后要把奥运安保“常态化”，就是奥运安保所有的设置要变成一般，特别是像北京这样的大城市的常规设置，那么作为新疆肯定也是一个很重要的地区。韶关事件发生以后，新疆当局有足够的时间做准备，因为韶关事件如此恶性的事件，造成多名新疆维吾尔族工人的死亡，而且后来在网上YouTub?e上面传播的录像，就是当时目击者用手机拍的录像，可以看到那种杀戮是非常残暴的。按照这种情况，消息传到新疆，新疆当局应该是有所准备，可能在新疆会有发生重大事件的可能性。从6月25日到7月5日，有整整10天的时间让新疆当局可以做准备的。 <br />
事实上也是如此，在7月5日乌鲁木齐开始出事是晚上，到了第2天早上就已经全部清洗掉了，当时发生了什么事情，再也没有人知道，如果这是称为一种骚乱的话，最大的骚乱在几小时就镇压下去了，也就是当时军警早就严正以待了。这个疑点是什么呢？既然军警早已严正以待，而且又有韶关事件以后的10天作为准备，怎么会让所谓的骚乱发生，是不是为了进行屠杀，而制造的骚乱，或者是故意让骚乱发生。韶关冲突以后的10天时间，当局对韶关如此重大的死伤事件，居然没有一个说法，这就不是一个简简单单的所谓不作为能够解释的。 <br />
<br />
第2个疑点就是关于境内和境外的问题，7月8日政治局召开了一个常务会议，他说7月5日的事件是境内外3股势力精心策划和组织的一起严重暴力犯罪事件。在这之前，中共已经把这个责任推到世界维吾尔人大会的主要领导人热比亚的身上，所举的例子是说热比亚在7-5事件之前打了2个电话，可能我想是他们监听到了2个电话。大家现在都承认今年的新疆事件，是六四以来最血腥的屠杀。如果说热比亚仅仅打2个电话就能引起六四以来，20年来最严重的死伤事件的话，在中共的理论里面所说的内因和外因的关系到哪里去？因为只有在新疆存在的严重的问题，才有可能被外来的势力所利用，这是中共的理论这么说的。 <br />
<br />
当然在这件事情上，有没有外来势力的介入，我仍然持怀疑态度。如果按照中共这种说法，它把7月5日的事件完全归咎于境外3股势力精心策划组织的话，就是把责任都推到热比亚身上去的话，那不是承认了在新疆，中共有这么多的警察，这么多的武警，有一个在那里统治了60年的党和政府，里面有这么多的官员，甚至包括有维吾尔族的中共的官员，和政府的官员，加上全部的宣传机构，他们对乌鲁木齐的影响力，或者是对新疆的影响力都抵不上一个热比亚，是不是中共就承认了这一点。要知道中共对新疆的居民，无论是汉族也好，维吾尔族也好，失去了控制力，失去了影响力，对中共来说这并不是一件值得夸耀的事情，并不是一件值得吹嘘的事情。这是第2个疑点，就是究竟有没有境外的势力介入，境外势力对新疆的局势的控制能力有多大。 <br />
<br />
第3个疑点就是新疆事件爆发的时候媒体的报道。这一次很多人都说，特别是中共方面自己说，中共的媒体报道非常迅速，还说立即邀请了境外的一些媒体来进行采访，但是它这个立即没有说是什么时候，因为大家知道，真正开枪屠杀的，造成最大伤亡的那段时间，那几个小时，并没有境外的媒体参加。同时却封锁了所有的民间的信息的发布，就包括Titter，包括Facebook，包括这些从新疆能够上网的，包括中国的饭否这样的网站，都被封锁了，所以可以说明一方面是中共自己的主要媒体的非常快的报道，另一方面却是非常快的封锁了民间所有的信息。要知道在这之前，石首事件当中，中共的媒体和民间的报道正好和这一次是相反的，当时是喉舌媒体的报道非常迟钝，而民间消息在网络上非常迅速的传播。这是正常的现象，因为石首事件是一个突发事件，当一个突发事件发生的时候，中共自己的媒体是反应不过来的，因为它无法预料这个事件所发生的规模有多大，对全国的影响力有多大。除非是中共知道要发生这么一件事情，或者是中共准备发生这样的事情，或者是中共策划发生这样一件事情，这才有中共媒体在第一时间，在第一线场报道的可能性。 <br />
<br />
否则的话，我们看过去几年，民间出现的重大的所谓群体事件，中共的媒体的报道都非常迟钝，迟钝是正常的，所以这次快并不表示中共的媒体有所转变，对于突发事件的快速反应不是一件容易的事情，作为中共媒体来说的话，不是一天或者一个月，甚至一年就可以改变过来的，不是说变就变的。所以我们只能说，这次是准备好了的，或者是导演好了的。这种事情并不是没有先例。我们可以看到对突发重大事件的最快速的报道，是天安门广场所谓“自焚事件”，我们现在都知道有非常多的证据，包括录像本身的证据，来证明中共策划了那次事件，所以才有媒体这么快的报道。在这之后，又出现过很多很多各类的突发事件，非常严重的事件，但都没有媒体的快速报道，没有媒体的在场。这并不能说明中共的媒体从天安门事件以后，它的现场的报道能力就减弱了，而只能说那次是预先准备的。而事后正常了，没有准备的，所以它反应不过来。 <br />
<br />
这次媒体报道的内容很显然就是一种血腥的渲染，就是把当时这种残酷的杀戮镜头拿出来渲染，渲染的结果毫无疑问的是煽动起一种情绪，一种对当时发生的事情的火上加油。真实发生了什么，现在谁都不知道。我们要知道六四屠杀的真相到今天20年过去了，还有很多没有揭开。但是至少说我们可以看到，从国内民众对于报道的反应，和对当时这个事件的反应来看的话，如果有人要激起汉族和维吾尔族之间的仇恨和冲突的话，那么这个媒体报道显然是起到了这样的作用，是增加了骚乱的可能性，而不是减少了骚乱的可能性，而不是维持了和谐或者是稳定。 <br />
<br />
下一个疑问就是前几天有人在问我，如果说这个事情是有人策划的，那么不是和当今中共现在的政策，就是“和谐”的政策或者是“稳定”的政策不符合了吗？这里我想和大家讨论一下，在中共所谓“和谐”政策下面，它的统治和“和谐”“稳定”，这两个哪一个重要，实际上这两点并不是永远相符合的。如果这两者有优先的话，那么显然稳定并不是第一位的，而是中共的统治是第一位的。中共不希望动乱，但是它不希望的是针对它统治的动乱，从历史上，中共对人民发动的动乱，包括历次政治运动甚至包括文革，并不在乎乱，中共从来就不怕乱，它绝不是不希望一般的民间冲突。 <br />
<br />
我们看到在中国大陆的大部分地区，主要是以汉族人居住的地区，民间其实没有特别重大的对立，所以你看到，在大陆的汉族地区，任何重大的冲突很快的就会转变成民间和官方的冲突，所以这就是为什么中共对在汉族地区任何动乱、任何冲突、任何群起事件，它都要反对，而且都要很快的去压下去。而在边远地区的话，比如西藏和新疆，中共并不是非常怕它乱，其原因是一个，它离统治中心非常远，即使那个地方发生动乱，并不直接影响到中共的统治，它在这里有两个做法来解决这个问题。它比内地多了一个选项，在内地它只有一个选项，就是血腥镇压，没有第二种做法，我们从过去几年的群体事件都可以看出来。但是在新疆、西藏，它还有另外一个选项，就是挑动民族之间的冲突，就是汉民族和藏民族，汉民族和维吾尔族之间的冲突，有的时候它是这两者兼用的，这是中共的统治和它的“和谐”“稳定”这之间有没有冲突，有没有矛盾，它都是一致的。在这两者不一致的情况下，它为什么并不在乎不稳定？这个所谓不稳定实际上是特例，不在汉区，不在内地的，在边缘少数民族地区的，这种情况，它才会不在乎，而且它并不在乎煽动起大的冲突来。 <br />
<br />
下一个疑点就是，这么重大的事件，是谁可能在这里面操作，因为这个我们现在没有很多直接的证据，但是如果我们分析的话，很多破案的时候，在案子完全破掉之前都没有直接的证据，但是你可以看到谁可以从中得到好处，谁有动机。如果我们把韶关事件和新疆事件综合起来考虑的话，我认为热比亚或者是其他的任何国际势力，除了后期的报道以外，都没有在事先和事发的时候介入，这是一个纯粹的中国国内事件。 <br />
<br />
由于这个冲突表面上，至少从表面上或者中共想让人家看到的是汉、维之间的冲突。在汉人当中并没有一个团体，任何一个团体任何一个组织，有能力或者是有意愿去策划韶关事件，并且把这个事件扩大到新疆，唯一有能力有动机的是中共或者是中共内部的一个派系。如果说这个事件，就像中共的喉舌媒体或者是中共在新疆的官员所说，是有人阴谋策划的话，那么有这个动机的，有这个能力的，不会是别人，除非你承认7月5日事件是没有阴谋的，如果你要说有阴谋的话，那么最有可能去制定这个阴谋和实施这个阴谋的，除了中共，现在没有一个团体、没有一个个人、没有一个组织能够这样做。 <br />
<br />
我们再来看一下，谁有可能进行这种跨省的操作，因为这个事件是从最南面、最发达的广东到最西北、最边远的新疆。从中共政治局的格局来看的话，从江的第三代所谓核心到第四代，它甚至连核心都不能形成，所以在中共政治局常委的分工里面，它是分工负责，也就是说每一个人只管自己所负责的这一部分。这个人在负责他这个部分的话，其他的人不能去干预，其他人也不愿意去干预。在四川地震的时候，国务院总理温家宝，他在政治局分工就是管这种事情的，所以你可以看到温家宝一个人乘飞机到处跑，到灾区、到震区，去慰问，去跟军人打交道，去发火呀，去摔电话呀，都是他一个人，其他人没有人管。 <br />
<br />
这就造成一个问题，重大事件的时候，一个是大家不能管。第二个，管的人可以为所欲为，如果归他分工的话，他的权力就非常大，如果不是造成重大后果，召开政治局常委会的话，这个错误几乎没有人会去纠正他，或者这个事情几乎没有人会去插手。 <br />
<br />
那么广东和新疆所发生的事情，都牵涉到了一个同样的问题，就是社会治安的问题。社会治安长期以来主要是归公安部管的，武警是属于双重领导，它属于中央军委和公安部双重领导，所以它也可以属于公安部这一部分的。还有一个就是社会治安综合治理委员会，和社会治安综合治理办公室。这个社会综治委是归中央政法委管的，而公安部也归政法委管，所以政法委在这里是能够管辖广东和新疆所发生的这个事件的唯一的一个中共中央的部门。政法委书记是唯一一个在中共中央政治局常委里面分管司法的人，就是周永康。因为韶关事件发生的时候，正好是广东的政法系发生重大政治危机的时候，广东的政法系很少有能力有力量去策划，或者是去管理韶关事件，所以一定有更高层的介入，因为广东的政法系正在处于人人自危的状态，那么广东政法体系的上层是什么，是中共中央政法委。 <br />
<br />
而新疆事件必须要有新疆的地方当局，包括中共新疆委员会，有新疆的政府，有中央实权人物的配合。在新疆当地被人称为“新疆王”的王乐泉，他是符合这个条件的，他在新疆是个地头蛇，甚至有人怀疑他曾经策划过新疆的两次爆炸事件，一次是朱F基去视察新疆，去调查王乐泉的问题的时候，那次发生了一次重大爆炸事件，所以为了他在新疆的统治他是可以去和当局配合的。和谁配合？王乐泉属于江泽民体系留下来的地方大员之一，他和江的系统的利益关系要比现在胡温这个系统的利益关系要密切得多。这是谁有这个能力进行跨省的操作。 <br />
<br />
我们再来看一下，谁有这个动机？在谈到动机的时候，我们就不得不谈到最先发生事件的广东，广东省的政法体系已经被清洗了一部分，其他的部分也正在处于人人自危的状态，这个是属于对江系人马的挤压，这是目前中国政坛出现的事情。在广东政法系的清洗当中，最先被清洗的是从公安部开始的，公安部部长助理，也就是周永康在公安部当部长的时候的亲信郑少东，原来来自广东省，从他开始清洗到了广东省的所谓“政法王”陈绍基，和陈绍基交出政法系统的领导职位以后，接手的王华元。然后又清洗到了深圳的许宗衡，矛头直指江泽民的亲信黄丽满，这是广东政法系正在被清洗的。 <br />
<br />
而在全国民间和官场上，普遍的对周永康不满的情绪，甚至包括国安对公安的不满，而公安在全国范围的滥权，全国范围之内社会治安的混乱，都是社会不满的重要因素，这些所有的因素矛头直指的，就是江泽民体系所留下来的中共中央政法委的书记周永康。所以如果制造事端对周永康来说的话，是最有动机的。 <br />
<br />
作为司法界酷刑、滥权、腐败，还有整个全国极糟极糟的治安状况的主要责任人，中央政法委的书记应该是一个问责的对象。在中国的政法系统，就是叫社会安全综合治理，这个系统当中有一个“问责制”，就是当社会治安出现问题的时候，这个当地的第一把手，党政一把手，就是党委书记和省长或者是市长，要负主要责任，而且都是有扣分的，叫作“一票否决权制”。但是在中共的政治当中，就有一个特别奇怪的现象，就是按说起来的话，当政法系统，特别是社会治安出现问题的时候，第一个被问责的就应该是政法委书记，因为一般人是不能干涉这方面的工作的，是他们管的。周永康当然就应该对现在的全国的政法系统的腐败和其他坏事情负主要责任，而他在担任中共中央政法委书记之前，就是罗干在当中共中央政法委书记的时候，周永康是公安部长，同时又是政法委的副书记，所以罗干当政的时候，他同样要负他的责任。 <br />
<br />
在中共政治当中有这么一种怪现象，就是一种出了事你就倒楣，这是那种社会综合治理的一票否决权制，这就是为什么当年吴官正在山东要拼命的镇压法轮功，是因为当时中共中央对吴官正问责，就是如果你这里到中央去上访的人数多的话，就要拿你问事。所以他就拼命的把人抓起来打，到打死为止，不让人家到北京去上访，这是一票否决权制，如果说上访的人多，一把手就要被撤职。 <br />
<br />
但是另外一边是一种怪现象，就是越出事他的地位越重要，而且他的地位越巩固，这就是我们现在所看到的公安部和中共中央政法委，所以他们不怕出事。这不仅是在公安部有这样的情况，不仅是政法委有这样的情况，在中国的很多由政策性所导致的政治运动有关的，或者是以政治运动的形式所搞的经济活动当中，都有这样的现象存在。我们以前曾经讨论过，水利部的利益在于长江的水患，而不是长江的水利，所以这才造成了水利部和当时在长江中下游的洪水经常泛滥的地区... <br />
<br />
早期在那些洪水爆发的地区有大片的沼泽地和低洼地，叫作泄洪区。就是当这个洪水太大的时候，把泄洪区打开，让洪水从旁边的这些泄洪区走。当地的政府和水利部串通起来，把大量的民众移民到这些泄洪区去，然后就向中央打报告，说是现在我们要更多的钱去修堤坝。为什么呢？因为如果不修堤坝的话，那些泄洪区现在都已经住满了人了，人民生命财产就会受到威胁。在这种情况下，是以向泄洪区移民，并以移民的生命财产做为要胁来向中央要钱。本来是他们犯的罪，结果却变成了他们向中央讨价还价的要胁的一个工具。 <br />
<br />
同样西藏问题也是如此，西藏问题在统战部有个西藏办公室，包括在西藏统治的藏族的那些高级干部，包括西藏自治区的主席、副主席，他们和西藏问题就紧紧挂勾在一起了。对于西藏问题的情报，对于西藏问题的处理意见，都是由他们提出来，所以他们会千方百计的要把西藏问题变成一个独立的问题。这就是为什么不管达赖喇嘛说多少遍，他们不要独立，只是想在一个中国的框架下实行高度的自治，但是在中共的宣传当中，永远把他们说成是藏独。这就是西藏问题的这些利益集团，只要西藏问题是一个独立的问题的话，那么他们就会不断的加强他们在西藏问题上说话的份量和他们的利益，他们是最不希望西藏问题得到和平解决的。 <br />
<br />
同样和这个类似的，还有一个610的问题，对于法轮功问题的收集情报和处理意见，以及每天的直接指挥，都是由610办公室的，这种就叫作利益冲突，他们会不停的制造出各种的问题来，来确保610办公室本身存在的合理性。 <br />
<br />
那么同样，中共中央政法委和公安部对于社会治安的问题，对于民族冲突的问题，它们有它们的利益所在，它们是唯恐天下不乱的。当这种事情出到很大的时候，就当时的韶关事件，和现在的新疆事件出来以后，中共中央政法委是不会被问责的，相反它们可以用这种事件来强调它们存在的必要性，它们存在的重要性，和加强铁腕统治是有必要的。 <br />
<br />
新疆事件中，它们的利益是以牺牲汉族普通民众的生命和财产，以及他们的安全作为代价的，同时也是以牺牲维族普通民众生命财产作为代价的。本来这两件事情，对韶关事件的调查和对乌鲁木齐，无论是汉族还是维吾尔族居民的安全的保障，都是应该政府做的，如果这么多汉族居民觉得他们的生命安全得不到保障的话，这是政府的责任。结果，本来应该处理这件事情的政法系统，即使是没有促成，或者策划事态发生的话，至少也是坐等事态的发生，或者是事件的爆发。 <br />
<br />
也就是说，在中国存在的这么一个系统，就是政法系统，它们的责任本来是维护社会稳定的，然而它们是一个最唯恐天下不乱的一个组织，只有从这个角度我们才能解释，为什么西藏问题、新疆问题总是得不到和平解决。如果说境外势力对它有影响的话，中共统治60年了，能够被轻而易举的挑动起来，发生这么重大的冲突的话，不恰恰证明中共的60年统治是失败的，中共的统治是非法的吗？</div>

							
							
							
							
