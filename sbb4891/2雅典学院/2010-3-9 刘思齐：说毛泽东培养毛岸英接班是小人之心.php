<br />
解说：在苏联近十年的生活，毛岸英已经欧化了，他的着装打扮，言谈举止，都让毛泽东看着多少有些不舒服。<br />
<br />
刘思齐：他刚回来，据说是穿着苏军的那个军服，西装啊，什么担担肩，伸伸手啊，就是外国人那些动作吧，然后，主席看到就不得劲，然后，主席就让他把那些衣服都换了。主席给了他几套主席自己穿的，那个旧的那个灰布军装，他穿得咣荡咣荡的，主席比他胖啊，他都愿意，他很高兴穿上。<br />
<br />
主席对他要求还是很严的，回来大概跟着主席没有吃几天，没吃几天饭在家里，就让他到大灶去吃去了。就是战士灶，跟战士一起吃饭。<br />
<br />
解说：毛泽东把儿子安排进了劳动大学，“师从”劳动模范吴满有，毛岸英肩背行李和一小斗米，步行十五里路来到了吴家枣园。<br />
<br />
毛新宇：因为我伯父长期生活在国外，离自己的祖国啊年代比较久远了，他比较熟悉苏联的党和国家的情况，对中国的革命实际情况和国情不是很了解，所以为了让他懂得中国的国情，所以才让他上的劳动大学。<br />
<br />
刘思齐：他等于是我们国家第一个山上下乡的知青，他就下去了，下去以后整个一个就是，应该说是一个农季，就是从开地、犁地，一直到播种、拔草、浇灌这些，到收割一直到入仓，他整个一个劳动下来没叫过一声苦，手上都是血，都是那个血泡，然后都是茧子。<br />
<br />
解说：毛岸英和乡亲们打成一片，跟他们学唱信天游，学扭秧歌，给他们讲苏联的战斗故事。<br />
<br />
刘思齐：他最大的收获，他觉得他了解了中国的农民，了解了中国的农民，知道他们想什么做什么，他们的生活习惯，他觉得中国的农民非常勤劳。<br />
<br />
解说：没多久，毛岸英就有了一嘴的陕北口音，手上长满老茧，看着羊肚肚手巾三道道蓝，一身黄土两腿泥的儿子，毛泽东满意地说，这就是你上劳动大学的毕业证。在延安，毛岸英遇到了他生命中最重要的女孩，革命烈士刘谦初的女儿刘思齐。7岁时，因为出演话剧《弃儿》，刘思齐成了毛泽东的干女儿。<br />
<br />
刘思齐：我演了一个戏，然后在台上妈妈给抓走了，当时我就觉得那是真的了，把我妈妈抓走了，然后又哭又叫的，后来就要我下台。下去就把我带到一个高高大大的一个叔叔跟前，然后那个叔叔就问我多大了，叫什么，问我愿不愿意做他的女儿。后来我母亲说，我看了看我母亲，我母亲点点头，我就叫了一声爸爸，后来我想那是，那个时候可能也就是，主席为什么一下子那么动情了，可能就是想到毛岸英、毛岸青。母亲牺牲了在上海，也是弃儿，等于也是弃儿，在流浪，可能想到这一些，想到自己的孩子了。所以就感觉到就有那种内心的有一种情感的一种波动在那里。<br />
<br />
解说：1939年，刘思齐和妹妹邵华，随母亲张文秋和继父前往苏联，途径新疆时被军阀盛世才扣留，被关押了8年之久。1946年，重庆谈判结束后，国民党释放了关押在新疆监狱的100多名共产党员，刘思齐一家才得以重返延安。平时刘思齐经常去干爸爸毛泽东家里做客，她先后认识了李讷和毛岸英。<br />
<br />
刘思齐：我带着李讷去延河边上散步，然后他从对面走来。李讷给我们介绍的，李讷跑去找他，叫他哥哥，然后李讷又把我介绍给他，说这是我哥哥，这是我姐姐，跟他讲这是我姐姐。当时我觉得，我就笑了。我说，哥哥姐姐还要妹妹介绍，要不然都不认识。<br />
<br />
解说：初见毛岸英时刘思齐刚16岁，他觉得这个比自己大8岁的哥哥和自己想像的并不一样。<br />
<br />
刘思齐：我觉得他就是一个很普通的干部，也没有多少洋气在里面，原来我觉得他是很洋的，从苏联回来的洋包子。<br />
<br />
解说：不久，刘思齐到北方大学学习，毛岸英在毛泽东的安排下也离开延安，先后到山西、山东等地参加土改工作。<br />
<br />
刘思齐：他去访问的都是最穷苦的，最穷苦的那个，最困难的，最穷苦的哪些人。他把他们很细的，就是每一个人的家庭收入，生活水平什么，他都非常，了解得非常清楚，写了那个工作日记写了很多。<br />
<br />
毛新宇：康生在土改过程中有一些，很具体的一些过左的错误，实际上都最早，我的伯父毛岸英抵制纠正过他，并且后来在给我爷爷的工作汇报当中，他都总结提到过康生在土改中的一些错误。可以说毛岸英的这个努力吧，应该，我觉得对于后我爷爷纠正啊，调整我们党土改的失误和错误是起了一定作用的。<br />
<br />
解说：1948年夏，参加土改回来的毛岸英与刘思齐，在西柏坡再次相遇，毛岸英向有着一双会说话的大眼睛的刘思齐，发起了爱情攻势。<br />
<br />
刘思齐：当时他就说，那意思就是说能不能够在一起生活。<br />
<br />
解说：突如其来的表白，让刘思齐有点懵。<br />
<br />
刘思齐：我说我们不了解，你是洋包子，你那么高的学问，我说我是土包子，我说差得太远了。<br />
<br />
解说：毛岸英并没有放弃，他给刘思齐说，我也是个苦孩子，也住过监狱，咱们是一根藤上的一对苦瓜。慢慢地毛岸英的直爽与执著打动了刘思齐，他们开始交往。<br />
<br />
刘思齐：他那个时候主要的就是想结婚，后来我也理解他了，他没有家一个人，生活也没有人照顾，说话没有人说，就是说回到自己房间里就一个人，他也感觉到挺孤独的。<br />
<br />
解说：毛泽东没想到二十年前，他的一句玩笑话竟然成真。<br />
<br />
刘思齐：在武汉，我爸爸把我妈妈带着去看主席，然后就看见那个，看见他们两兄弟，岸英、岸青两兄弟。两兄弟在那跑来跑去的，我妈妈说岸英还给她抓了两把花生，那小手抓得满满的送过来，我妈妈很喜欢这个孩子，就说这个孩子真可爱什么的。当时主席还说，说那你们俩赶快生女儿，我们就那个什么，将来就亲家，结果我爸爸还说，说我们还没结婚呢，把我妈妈说得挺不好意思的，说我们还没结婚呢。<br />
<br />
解说：1949年10月15日，毛岸英和刘思齐举行了婚礼。<br />
<br />
刘思齐：上午还在上课呢，下午就回去，回到我妈妈那去，那就弄个车把我接到中南海。<br />
<br />
解说：虽然时间已经过去了六十年，但刘思齐至今依然记忆犹新。<br />
<br />
刘思齐：他好像就是穿着一套那个照相的那个黑色的，好像那种很薄的那种呢子的衣服，那是他工作的时候的工作服等于是。我没衣服，我没衣服，我记得我妈妈找了半天，找了一块棕色的条绒，赶着做了一件列宁服。裤子都是我原来穿的什么裤子都没换，就那样。后来在我辫子上，我扎两根辫子，在辫子上给我系两个蝴蝶结，还有李讷不知道是谁，不知道从哪折了一朵花戴到我头上。<br />
<br />
解说：因为毛泽东要求简单操办，当天的酒席只有三桌。<br />
<br />
