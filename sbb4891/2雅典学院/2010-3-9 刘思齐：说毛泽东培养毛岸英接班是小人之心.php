																											<div id="postmessage_509545" class="t_msgfont">刘思齐：说毛泽东培养毛岸英接班是小人之心 <br />
2010年03月09日 10:28 凤凰网专稿 <br />
<br />
核心提示：刘思齐：有文章说毛泽东送毛岸英上前方是为了培养他，锻炼他，要他将来接班，我觉得这种想法，是以小人之心度君子之腹。为什么，毛泽东从他参加革命起，从他干革命起，他就没有离开过战争。他从秋收起义到他上井冈山，到他离开井冈山，带着红军二万五千里长征，这是打出来的。我们几万人，多少万人到延安才剩了多少人，才剩了两万来人，我们牺牲多少烈士，他难道不知道战争是要死人的吗？你毛泽东不派毛岸英去，也不会有人说他为什么不派儿子去。<br />
<br />
<br />
凤凰卫视3月6日《我的中国心》以下为文字实录：<br />
<br />
曾子墨：幼年流落上海，少年留学苏联，青年学农延安，而后在抗美援朝战场上，他的生命永远定格在了28岁。57岁的毛泽东得知消息后，沉默良久，这位英年早逝的年轻人一生短暂而又传奇，经历丰富而又坎坷，被彭德怀称为志愿军的第一个的志愿兵，他就是毛泽东的儿子毛岸英。<br />
<br />
解说：1921年，湖南青年毛泽东迎娶了恩师杨昌济的爱女杨开慧。一年后，他们的儿子降生在长沙清水塘畔，起名岸英，后来毛泽东回忆起毛岸英的童年趣事时，总忍不住会哈哈大笑。<br />
<br />
毛新宇：就把我爷爷奶奶的鞋啊，穿着在雨地里撮雨，然后我伯父还喊，开船啦，开船啦，哥儿俩就非常兴奋。<br />
<br />
解说：1927年，蒋介石叛变革命，中共中央派毛泽东领导秋收起义。这时的长沙笼罩在白色恐怖中，处境危险的杨开慧带着岸英、岸青、岸龙三兄弟逃到板仓老家。三年后，身在井冈山的毛泽东从国民党的报纸上看到了一条让他心痛的消息，湖南军阀何键悬赏一千大洋抓拿杨开慧。1930年10月，杨开慧和年仅8岁的毛岸英被押进了长沙协操坪监狱。<br />
<br />
刘思齐：从监狱里头把他母亲押到刑场的时候，他就守在他母亲身边，然后，他抱着他母亲不让走，那小孩又哭，就痛哭舍不得，舍不得敌人把他扔到，就是说把他撕开，从他母亲身边撕开，扔到一边去，扔到那个监狱那个房子那个角落里，把他母亲押走了。<br />
<br />
他可能在监狱里头，还住了半个多月20天才放出来，整个在监狱里头，他就是不吃不喝，大病了一场，出来的时候就一把骨头了已经。<br />
<br />
解说：杨开慧被害后，毛泽东愤笔写下“开慧之死，百身莫赎”八个字，而亲历母亲之死的毛岸英在之后的二十年里，更是没法停止对母亲的思念。<br />
<br />
刘思齐：有一天他就，我后来想，可能那一天就是他母亲牺牲的日子，因为结婚不久嘛，结婚不久他就站在窗户前面的，非常沉默地站在那儿，后来就说，他说我妈妈是一个伟大的母亲，是伟大的女性，然后就流眼泪。<br />
<br />
解说：在地下党的安排下，毛家三兄弟改名为杨永福，杨永寿，杨永泰，由外婆向振熙带到上海送到“大同幼稚园”。1931年5月，毛岸龙因患痢疾去世，由于上海时局动荡，幼稚园的孩子们被迫疏散。“红色牧师”的董健吾把毛岸英和毛岸青领回家，但不久，兄弟二人就因忍受不了牧师家里的生活而离家出走，流落街头，相依为命。<br />
<br />
毛新宇：我父亲在流浪的时候，因为写革命标语，被反动巡捕给打，打了负了重伤是我伯父救他。<br />
<br />
刘思齐：他带我去看《三毛流浪记》，然后看的时候，他就非常激动。他非常激动，看完了以后，他一直坐在那不动，人家场子里人都走完了，他还坐在那。后来我告诉他，我说人都走了，我们走吧。然后他就站起来的时候，他就跟我讲，他说我，三毛所有的生活我都经过，捡垃圾，捡烟头啊，卖报纸，给人家当小童工啊都干过，兄弟俩当童工，还有推车，就是在什么白渡桥，他们就在后面推，推上去了，下去的时候拽着那个车，不让它下滑太快。就这样一次大概是一个铜板，两个铜板。<br />
<br />
曾子墨：在上海，毛岸英、毛岸青流浪了整整五年。这期间他们的父亲毛泽东率领红军在江西瑞金抗击着国民党军队，连续的“围剿”并艰苦跋涉二万五千里胜利到达陕北，像他们的父亲一样，两个身处绝境的少年虽然历经狂风暴雨，但是仍然顽强的生存下来。1936年，根据潘汉年的指示，上海地下党找到了毛氏兄弟，并且辗转将他们送到苏联。<br />
<br />
解说：1937年中共驻共产国际代表团副团长康生，把毛岸英、毛岸青送到位于莫斯科市郊的莫尼诺国际儿童院，当时儿童院里中国学生占了百分之二十，大多是烈士子女和共产党领导人的孩子。结束了流浪生涯，开始了留学生活，毛岸英曾说过那种感觉像是掉到了蜜罐里。<br />
<br />
刘思齐：他去了以后，他觉得非常好，非常幸福了。他曾经写了一篇文章，写了一篇文章叫什么，我忘了，他就是说，我没有想到世界上还有这么幸福的地方。就是大家都觉得这是天堂了，已经是天堂了。<br />
<br />
解说：赵施格，中共早期领导人赵世炎的儿子，在他印象里，初到儿童院的毛岸英和他们没有什么两样，刚开始与大家沟通起来还有些困难。<br />
<br />
赵施格（毛岸英儿童院同学）：一起玩，一起学习，他岁数大一点，比我们大一点，也没看出来他有什么突出啊，是吧。我们还比他突出，因为我们会说俄语，他们不会。<br />
<br />
解说：1938年，在看到岸英、岸青的照片之后，念子心切的毛泽东给儿子写了第一封家书。亲爱的岸英、岸青，时常想念你们，知道你们情形尚好，有进步，并接到了你们的照片，十分的欢喜。1941年初，毛泽东给儿子寄去了一些书，并附信一封，信中建议儿子趁着年纪尚轻，多向自然科学学习，少谈些政治，政治是要谈的，但目前以潜心学习自然科学为宜，社会科学辅之。<br />
<br />
1941年6月22日，希特勒发动了苏德战争，国际儿童院的学生们也参加到各方面的保卫工作中，他们不但要种菜，挖防坦克壕，还要上军事课。<br />
<br />
赵施格：怎么用武器，就是机关枪也好，步枪也好，怎么扔手榴弹，什么都学会了。<br />
<br />
解说：怀揣着保卫社会主义的信念，很多学生都想加入苏联红军，投入卫国战争。19岁的毛岸英就是其中之一，但是，苏共中央不同意让国际儿童院的外国青少年上前线。<br />
<br />
毛新宇：他强调要求参军，其实也很不容易，一开始也没被批准，他上军校，后来遇到一个苏联红军将军很喜欢他。他才先上的是苏联红军的军校，后来才真正的从军校很快毕业以后，才当了苏联的红军。<br />
<br />
解说：1943年，毛岸英从莫斯科列宁军政学校毕业，被授予中尉军衔，并加入联共。1945年，苏军开始了大反攻，毛岸英获准参战，担任坦克部队连队指导员。<br />
