													<h2>《毛岸英在朝鲜战场》的另一种解读</h2>
						
						
																											<div id="postmessage_511011" class="t_msgfont">毛泽东是多子之人。杨开慧生育了三个儿子，贺子珍生了十个子女，江青生了一个女儿，但存活成人的，只有两子（岸英、岸青）两女（李敏、李纳）。岸青有精神病，身体健康的岸英就成了毛的唯一希望。关于毛岸英的生平，资料很少，其实他从苏联回国后到在朝鲜牺牲，也有近五年的时间，应该留下一些事迹才对。武立金著《毛岸英在朝鲜战场》（作家出版社，2006年9月第一版）提供了毛岸英的不少资料，尽管作为主旋律文学，作者着力于歌颂毛岸英的超凡品质，特别将援朝牺牲这一段作为华彩乐章，但还是提供了大量历史事实。长期阅读官方宣传材料，使我们养成了一种去伪存真的能力，学会透过人为的光环，注意书中有意无意透露出的一些细节，这些细节才是最真实和最有价值的部分。汇总、分析这些难得的资料片段，我们看到了一个接近真实的毛岸英。<br />
一、&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;来去随意，做工作浅尝辄止<br />
毛岸英1946年初回国，到1950年10月赴朝，近五年多时间里，似乎没有比较稳定地从事过什么工作，正式披露的工作经历，都是短短的几个月甚至几十天。并且行动自由，没有什么“单位”的约束。<br />
比较著名的一个经历是上“农业大学”。毛岸英刚回到延安，毛泽东要求他跟随农民劳模吴满有学农活。这段日子，其实只有五十多天，充其量也就是一个乡村夏令营而已（p122），但后来凡提起毛岸英，都要大书特书这一段。<br />
进北京后他的工作岗位是中央社会部（中国克格勃）部长李克农的秘书兼翻译。在这个岗位上，他似乎也很散漫。按说秘书工作是非常忙碌的，通常没有自己的时间。但毛岸英在1950年5月初有一次悠闲的长沙探亲。“这次南下是公私兼顾，他随苏联代表团来到武汉，给李克农当了几天翻译后便匆匆赶往长沙探亲”。（p7）<br />
在韶山，乡亲毛贻泉找他要帐，说是30年前毛泽东欠下100大洋至今未还。毛岸英没钱，找省委书记王首道“借钱”还上了（p11）。一次探亲扫墓，他竟然盘桓了一个多月，直到6月25日朝鲜战争爆发，李克农要秘密访苏，发电报来催，他才回京。回京的时候，这个28岁的青年干部乘坐软卧列车，“毛岸英品尝一口杯用长沙水泡出的君山毛尖茶，然后仰坐于沙发上，开始翻阅当天的报纸”（p9），这估计是湖南省委提供的特殊交通安排。从向省委书记借钱、住省委招待所、坐软卧这些情节，可以看出毛岸英此行公开打了“父皇”旗号，而党政大员也丝毫不敢怠慢这位第一公子。<br />
赴朝之前，毛岸英在北京机器总厂做党总支副书记，这是毛岸英比较正式的一个工作履历，是毛岸英闹着要去工厂的情况下，周恩来亲自安排的。按说此时韩战已经爆发，社会部无论是情报工作还是对苏联络都非常繁重，他为什么要离开部长秘书的岗位，去一个完全不能发挥自己俄文优势的北京机器总厂呢？这本书没有揭开这个迷团。从1950年8月中旬到10月8日，他在北京机器总厂只干了不到两个月（p18）。10月8日他跟彭德怀去东北，没有向厂里作任何交待；10月14日随彭回京，次日即将再赴东北、朝鲜，他才匆匆到工厂交待说社会部有任务，他要去工作一段时间（p85）。<br />
将近五年时间，我们看到毛岸英的工作岗位飘忽不定，没有看到他在哪方面做出扎实的业绩来。<br />
二、 赴朝出于政治需要，只准备三五个月就回来。<br />
毛岸英赴朝是自己提出的，还是毛泽东的旨意？书中记述，10月5日，中央决定了派兵援朝之后，毛泽东对卫士小李有一番对话：“我积极主张抗美援朝，我的儿子不去，谁还能去？我想把岸英交给彭德怀，一起去朝鲜打仗，你看好吗？”毛又说“跟彭德怀同志在一起，学些军事知识，对他的将来会很有用的。”（p36）当晚或次日晚，毛泽东叫回岸英：“今天，我让你回来，就是想和你谈谈当兵的事”（p43）。可见，岸英参军赴朝，是毛泽东做出的一个安排。其出发点，一是表示带头，二是为了岸英的“将来”。<br />
毛岸英到朝鲜干什么，也是毛泽东安排好了的。10月7日，聂荣臻打电话给毛泽东报告说：“彭老总明天就要带他的一班人马去沈阳开展工作了，可是他的俄文翻译到现在还没有找到”，毛说“那就不用找了，让岸英去吧，我通知他。”（p54）可见毛并不想让岸英上前线，而是细心地替他考虑了既安全、又能掌握核心情况的岗位。<br />
毛岸英当时作了多长时间的打算？书中没有正面说。但他在向岳母张文秋告别时说过“多则半年，少则三月”（p96）；他的衣服、被褥、书籍还在北京机器总厂没有收拾，他说，“先放在这儿吧，我还要回来的”（p85）。最能说明问题的一个情节是，1951年1月2日，此时毛泽东还不知道岸英牺牲，“正在看文件的毛泽东听说叶子龙来了，头不抬眼不动地说：‘子龙，我正要找你呢！把岸英调回来吧，你看他把材料写成这个样子，不但没有进步，反而退步了！’”（p251）此时距毛岸英“报名参军”，不到三个月，距赴朝才两个月零十天。如果他没有牺牲，凯旋回京，正好应了他对岳母说的“短则三月”。<br />
三、 爱炫耀，性情浮躁<br />
毛岸英在朝鲜志愿军司令部总共待了34天，但大家都知道他是毛泽东的儿子。本来他的身份是保密的，但他自己基本上逢人就说“不错，我的父亲是毛主席”（p194），毫不忌讳。他平时腰里挂着一支小手枪，遇到人问时，就拔出来说“这支手枪有点来头，是斯大林赠送的呢”。大家惊羡道“你去过苏联？见过斯大林？”他就开始介绍在苏联呆了十年，参加苏联红军打到柏林，受到斯大林的专门接见，斯大林送他手枪并问他为什么不找个苏联姑娘做妻子等等。（p210）试想，这一番经历，别说一般干部战士，就是彭老总，也望尘莫及呀。给工农出身的战友们讲这些，是其炫耀性格的典型表现。<br />
其实，他所谓的苏联红军生涯，如同他的农民大学、工厂书记一样，也是浅尝辄止的经历：“1943年，毛岸英被保送到莫斯科列宁军政大学学习，考虑到他是毛泽东的儿子，苏军破例授予他中尉军衔。一年后，他又进入苏军培养高级参谋人员的最高学府伏龙芝军事学院深造。毕业后，毛岸英被任命为坦克连指导员，参加了苏军的大反攻。”（p17）屈指算来，这时已经是1944年底或者是1945年初了，而攻克柏林是1945年4月30日，所以说毛岸英这一段战争生涯最多也只有半年天气。而且由于“中苏两党有一个协议，不让中 共领袖的孩子参战”（p17），故“坦克连指导员”的安全是有保证的。<br />
四、 对“志司”首长颐指气使<br />
第一次战役之后，毛岸英与三十八军军长梁兴初有一次对话：<br />
“梁军长，你那里要人不？我到你们军去行不行？”<br />
“你想干什么？把你安排到作战科行不行？”<br />
“要是还在机关工作我还到你那儿干什么？在志司作战室不是一样嘛！”毛岸英不以为然地说。<br />
“那你想……”梁兴初不解地问。<br />
“我想下基层！”毛岸英像他父亲那样把手一挥，“从营长干起，你给我一个营怎么样？”<br />
好家伙！梁兴初为之一惊，他被毛岸英这股子气势给镇住了。……谁知彭老总是怎么打算的？只好支支吾吾地说：“那好，那好……”<br />
“你答应了？君子一言，驷马难追。我什么时候去报道！”毛岸英认真了起来。<br />
“我是求之不得，只怕彭总不放你走，下面危险大哟！”梁说。<br />
“你去和彭总讲一讲嘛！就说我有打仗的经验，我在苏联打过仗，参加过卫国战争。”<br />
“和彭总讲，那我可不敢……说梁兴初你怎么挖我的墙角？那我可吃罪不起。”<br />
“嗨，你们怎么都怕彭老头？”毛岸英一捋袖子，“好吧，我去找他谈”。（p159）<br />
这哪里是司令部的一个小秘书与主力军军长的对话？“不以为然地说”、“像他父亲那样把手一挥”、“一捋袖子”，这几个动作形像地反映了当时毛岸英的心理状态。而面对一位高级将领，称全军统帅为“彭老头”，并非无知，而是无畏――“只缘身在最高层”啊。<br />
另一件事，“毛岸英和彭德怀下棋，经常为悔一步棋而争得面红耳赤不亦乐乎”。事后其他首长委婉地劝说他不要这么认真，要让彭总下棋后心情放松才能更好地指挥作战（p136)。过去看过一个回忆录的描述是，毛岸英当场说“他M的彭老总你又悔棋啦”，彭则笑呵呵地赖帐，洪学智则在身后用腿碰毛岸英，示意他尊重彭总。两相印证，毛岸英在彭的面前，基本上是“童言无忌”，并不把彭当首长对待。<br />
最典型的事件是书中151页的记载。第一次战役结束后，彭德怀主持第一次志愿军党委扩大会议，实为最高作战会议。会上彭发火痛骂了三十八军军长梁兴初，说出“违反军令，按律当斩”的狠话来，全军高级将领俱噤若寒蝉之际，彭德怀开始布署第二战役的打法：“我的意见是先退，我们的主力从现阵地后撤三十至五十公里，让麦克阿瑟以为我们怕他。这样，他就会更猖狂，造成前军突出，我们就可以寻隙穿插，分割包围……”这时，令所有人意想不到的事情发生了：毛岸英“离开会议桌直走到彭德怀对面，指着作战地图慷慨陈词：‘我看应该向南进攻！兵书上说：善战者，见利不失，遇时不疑。敌人不是跑了吗？不是败了吗？我们为什么不乘胜追击，而要后退呢？’”所有的与会者都大为诧异，私下议论说“那个小翻译胆子不小，竟敢在彭总发火的时候说三道四，这样重要的会议，哪有他讲话的资格？”此时的毛岸英，显然忘记了自己只是一个秘书兼翻译，而把自己当成了监国的太子或者是钦差大臣。<br />
五、 生活散漫，违反防空纪律导致丧生<br />
毛岸英好睡懒觉，在书中有多处反映。“一觉醒来，天已大亮……毛岸英不禁心中自责‘日上三竿我独眠，太不应该了。’”（p70）作战室主任张养吾回国前给毛岸英的临别赠言是“按时起床、按时就餐、按时防空”（p201）。在支部会上，作战处副处长成普提意见说“有一次毛岸英起床晚了，我们等他去吃早饭，没想到刚端起饭碗飞机就来了，我们四个人被堵在屋子里，只好一个人蹲在一个墙角落，像块奠基石。”（p202）<br />
毛岸英牺牲是在11月25日。此前志司为防空袭，“作出了三条规定：一是天亮前一定要吃完饭，二是天亮后不准冒烟，三是都要疏散防空。”彭德怀也强调“你们这些年轻人要注意防空，不能有任何侥幸心理，该进洞而不进洞的是纪律问题”（p218）。<br />
当天早上毛岸英由于晚起床，又没有吃上早饭。“躲在防空洞里的毛岸英伸头看了一下天空，还不见飞机的影子……此时已是饥肠辘辘了”。十点过后，毛岸英对高瑞欣说要回作战室，高说“等一等吧，警报还没解除呢”，毛岸英说“不用怕！我看飞机一时来不了，就是来了，哪会偏偏炸中这个地方。当年国民党的飞机经常轰炸延安，可爸爸忙于工作，就是不进防空洞……不也没事嘛！爸爸的榜样，儿子不学谁还去学。”（公然违纪，都要打“爸爸”的旗号，这样的公子哥真够志司首长头痛的。）说着毛岸英已经冲出了防空洞，高瑞欣等只好跟着他到作战室热饭。（p220）<br />
可惜毛公子没有“爸爸”那么好的运气，11点多，美军四架B-26轰炸机掠过大榆洞上空，马上又返回，是否因为看到了毛岸英热饭的饮烟，不得而知，但这一次投下了几十枚凝固汽油弹，准确地命中了作战室。幸存者成普事后说，“当时毛岸英正在炉子旁吃东西，我在门外看到飞机正在扔炸弹，就喊快跑，可是毛岸英和高瑞欣都钻在桌子底下躲炸弹……要是早跑出来也许就没事了。”（p247）<br />
可见，这是一次完全可以避免的事故，由于毛岸英违反防空纪律，不但导致志司作战室被轰炸，自己身亡，而且连累优秀的机要参谋（周恩来语）高瑞欣牺牲。<br />
毛岸英1922年10月24日出生，5岁离父，8岁失母，在学习知识和形成世界观的最重要阶段，基本上是在颠沛流离中度过，其中至少有五年是在上海流浪，直到十四五岁时被送去苏联。后天的不足使他在知识和性格上存在一些缺陷，也就是不足为怪的。但官方的宣传在神化毛泽东的同时，对毛岸英也进行了神化，误导我在很长时间里都认为毛岸英是个完美的革命青年，甚至认为如果他不牺牲，将可以成为制衡江青的因素，不致于让文 革发展到那样的程度。感谢武立金先生，他提供的这些生动真实的细节，让我们对毛岸英有了近距离的观察。如果毛岸英在朝鲜不出意外，对他在后来中国社会的作用，也不能有过高的期望。</div>

							
							
							
							
