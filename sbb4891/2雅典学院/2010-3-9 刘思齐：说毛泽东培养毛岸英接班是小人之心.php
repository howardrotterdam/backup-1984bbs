<br />
刘思齐：什么事情都想做得最好，但是呢，他又总觉得，总是对自己不满意。总觉得没有做到，就是还没有做到位，总觉得不能给主席丢脸。<br />
<br />
解说：然而毛泽东和杨开慧老家的一些亲戚，并没有像毛岸英一样严于律己。建国后有的亲戚伸手来讨官，还送了许多土特产到北京。<br />
