毛新宇：现在我大姨回想起来难受，都后悔自己当初太迟钝了，没有理解这个自己丈夫的用意。<br />
<br />
解说：深夜，毛岸英才起身离开病房。<br />
<br />
刘思齐：他就不让我送，我说还是送一送吧。送到门口他就几次走都走不开，就是好像，后来就转过去就要走，要走又转过来给我鞠了一个躬，深深地鞠了一个躬，就回头就走了，当时我就有点发懵。因为从来，结婚以后他出过几次差嘛，出过两次还是三次差都没有这样，都没有说鞠个躬，当时我还有点发懵，我说出差鞠什么躬啊，我说。那时候也没有多想，也没有多想。<br />
<br />
解说：随后，毛岸英来到了岳母张文秋家，请她照顾弟弟毛岸青。<br />
<br />
毛新宇：也是蒙蒙胧胧，我爸爸最先喜欢我母亲，那么这个是后来发展，我母亲后来也爱上我爸爸。那么当时来说伯父知道，知道了我爸爸的心理，喜欢我母亲邵华，所以他在他预计到他上战场可能要牺牲，他就事先嘱咐，除了说让我的外婆照顾好我父亲，他提到了，其实他是希望我父亲跟母亲结婚的。<br />
<br />
解说：毛岸英向岳母借手表，张文秋把史沫特莱送的手表给了他。<br />
<br />
曾子墨：彭德怀讲过这样一段意味深长的话，困难当头挺身而出，这不是每个人都能做得到的。有些高干子弟，甚至高级干部本人就没有做到，但毛岸英做到了，毛岸英是坚决请求到朝鲜抗美援朝的。1950年10月8日，毛岸英随志愿军出征，在大榆洞矿区志愿军司令部，毛岸英有了一个新的角色，彭德怀的翻译兼机要秘书，但除了彭德怀、洪学智等极少数人，大家都不知道这个高高大大的年轻人的真实身份。<br />
。。。。。。。。。。<br />
<br />
<br />
解说：为了帮助儿媳走出痛苦的阴影，毛泽东让刘思齐去苏联莫斯科大学学习，他还一直敦促刘思齐再婚开始新的生活。1962年，刘思齐与空军学院教师杨茂之重建了家庭。<br />
<br />
如今，刘思齐正在撰写回忆录，记录她和毛岸英、毛泽东一起走过的那段岁月。<br />
<br />
刘思齐：有文章说毛泽东送毛岸英上前方是为了培养他，锻炼他，要他将来接班，我觉得这种想法，是以小人之心度君子之腹。<br />
<br />
为什么，毛泽东从他参加革命起，从他干革命起，他就没有离开过战争。他从秋收起义到他上井冈山，到他离开井冈山，带着红军二万五千里长征，这是打出来的。我们几万人，多少万人到延安才剩了多少人，才剩了两万来人，我们牺牲多少烈士，他难道不知道战争是要死人的吗？你毛泽东不派毛岸英去，也不会有人说他为什么不派儿子去。<br />
<br />
那么他会为了儿子去锻炼，就把他送到前线去，他不知道战死，他是把他送到最危险的地方去，随时都失去生命的一个地方，他能不知道吗，子弹会长眼睛吗？<br />
<br />
曾子墨：毛岸英牺牲后，曾有人提议，将他的尸骨运回北京安葬，毛泽东没有同意，他引用东汉著名将领马援的话说“青山处处埋忠骨，何须马革裹尸还”。为了中国的革命事业，毛泽东先后失去六位亲人，而毛岸英的牺牲无疑是年仅60岁的毛泽东心中最大的痛。<br />
<br />
“为有牺牲多壮志，千古留名人世间。”2009年毛岸英入选“100位新中国成立以来，感动中国人物”，再一次唤起人们心中那份并不遥远的记忆。<br />
