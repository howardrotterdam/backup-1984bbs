今天上午有朋友给我发短信说，我们的《刺陵》上线了，你有空去看看。<br />
<br />
今天下午有另外朋友给我发短信说，我们的《风云2》上线了，你有空去看看。<br />
<br />
于是，我去了电影院，买了一张《三枪拍案惊奇》的票。<br />
<br />
 <br />
<br />
不得不说，我对一个拍出过《活着》的导演还是抱有期待的，尤其当这个导演选择了一个这么土的电影名字，选用了这么土的一堆演员的时候，我以为他要反时尚的玩些什么。<br />
<br />
看完我发现，是真的很土。而且，我认为，他们的内心其实是奔着时尚去的，也许他们认为，这就是现阶段的时尚。<br />
<br />
 <br />
<br />
我猜想这个电影的诞生是因为本山传媒的赵本山找到了张艺谋，两人拍一拍身上的土，坐在炕上说，艺谋，我们合作拍一个片子呗，你看，小沈阳他们这么红，你一用他票房也有保证，我这里演员也可以再往艺术的方向整一整。<br />
<br />
张艺谋说，这个好啊，整一个，我就喜欢科恩兄弟，这次我玩一个HIGH的。<br />
<br />
于是他们弄出了剧本，但是在操作的过程中，张艺谋开始担心了，这未免也太土了，我们必须要加一些年轻人喜闻乐见的时尚元素进去。于是张艺谋开始问身边的人，最近流行些什么啊？<br />
<br />
张艺谋身边的人拍一拍身上的土，说，最近流行武林外传，里面好多词都是网络流行词。<br />
<br />
张艺谋一想，网络好啊，这就是时尚。<br />
<br />
 <br />
<br />
要知道，这些人们拍电影和化学家一样，最讲究的是“元素”两字，全剧组拍一拍身上的土，一思量，喜剧元素，西方元素，时尚元素，春晚元素，二人转元素，网络元素都齐了。于是，他们一头扎进了大西北的土里，开始拍这部电影。<br />
<br />
 <br />
<br />
看完以后，不得不说，小品有的时候就是小品，当你做到了电影的长度，它充其量只是一个大品，依然不是电影。电视剧有的时候就是电视剧，当你用了电影的配置，它充其量只是一个电视电影。从张艺谋对于高速摄影机的爱不释手，我可以理解为什么他依然固守着已经落伍十年的美术风格不放，我建议张艺谋和他的团队继续固守，千万不要多想什么与时俱进，说不定某一天，时尚的轮回还是会转到他们那里去的。<br />
<br />
 <br />
<br />
整部电影我给的分数是1分，这一分是对于张艺谋放弃人海战术和片子中某些演员的表演还算不错的鼓励。这是一部比较适合在三线城市的县城里播放的电影。而看完电影我唯一的感受就是赵本山培养的演员都不大会走路。</div>

							
							
							
							
