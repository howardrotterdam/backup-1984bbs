													<h2>张艺谋《三枪》让各方失望</h2>
						
						
																											<div id="postmessage_369209" class="t_msgfont">北京12月11日电／昨晚，张艺谋新片《三枪拍案惊奇》在重庆上映，记者看完之后随机询问了部分观众，出人意料的是，张艺谋奥运复出的第一部影片从演员到台词，从风格到剧情，遭到大部分人的齐声质疑，被称为“无场面、无悬疑、无笑料”，有人甚至取笑称这是“张艺谋史上最烂影片”。<br />
<br />
　　重庆晚报报道，《三枪》改编自《血迷宫》，号称悬疑大片，幸好科恩兄弟看不到这部片子，否则会捶胸顿足。<br />
<br />
　　讲故事一直不是张艺谋的特长，这次依然没有任何改进。面馆老板发现老婆闫妮和伙计小沈阳偷情，便请孙红雷来杀人，结果孙红雷反杀了老板以图卷走他的钱财，小沈阳和闫妮发现老板被杀，孙红雷便要杀人灭口，慌乱中闫妮一枪崩掉孙红雷，全片结束。情节简单到比早期《故事会》上的故事都不如。<br />
<br />
　　张艺谋花大价钱买来《血迷宫》的改编权，却仅仅借鉴了故事的基本框架，全片用了前半小时的时间来斗嘴，到后面开始进入故事，老板买凶、孙红雷杀人、闫妮自救，一条非常简单的线索，用直接叙述的方式讲出来，没有迂回、没有暗线，让人感觉在看法院的卷宗，完全抛弃了《血迷宫》的巧合、悬疑特点。<br />
<br />
　　风格乱　何处不忽悠？<br />
<br />
　　《三枪》号称投资1亿，“国际大导演张艺谋的华丽转身之作”。从片子来看，这些说法言过其实。<br />
<br />
　　大场面一直是张艺谋的特长，在《三枪》里他确实转身了，全片包括路人甲官兵乙在内的群众演员，加起来才19个人；道具方面也只有简单的一栋房子、一架马车、一把手枪、一门炮、一个保险箱、一把刀、一把剪刀、几件衣服，大部分戏都是在面馆里拍摄，所谓苍茫的大西北场景，不过是作为外景摇了几个镜头而已。若说投资上亿，很难让人相信没有水分。<br />
<br />
　　色调也一直是张艺谋的长处，片中他采用了“红男绿女”的夸张服饰搭配，不过几乎都是二人转的服饰风格，这会让南方观众很不适应。此外西北高原的外景色调还算漂亮，但与片子的“杀人”剧情显得格格不入。<br />
<br />
　　整个片头就是一扇木门，定在屏幕上一分钟，在那上面打出演职人员名单。片子剪辑有明显的脱节现象，甚至出现了一秒钟的黑幕，这样的低级错误实在让人难以容忍。由于首次采用数字摄像机而没有用胶片，影片画面也不够通透清晰，像蒙了灰。<br />
<br />
　　演员板　何人有演技？<br />
<br />
　　制片人张伟平很商业地请来了赵本山、小沈阳、孙红雷，就是希望三人的人气能够拉动票房。但结果恐怕要适得其反了。<br />
<br />
　　从表演来说，全片水平最高的是闫妮，但也就是《武林外传》佟掌柜的翻版。小沈阳的表演完全没有脱离小品痕迹，木讷时木讷、夸张时夸张，几乎无亮点可言。孙红雷因为从头到尾穿戴着铠甲，根本无法表现面部表情，一直板着一张脸，很是乏味。赵本山的斗鸡眼将军倒是个亮点，可惜只有三分钟的戏。<br />
<br />
　　喜剧是本片的一大卖点，其台词也是《三枪》唯一勉强撑得起来的地方。片中采用“别糟蹋了青春两字，你已经立秋了”之类的小品式台词，但其笑点无论如何超不过赵本山一部春晚的小品。将小品10分钟的台词分散到一个半小时里，并且是在一部买凶杀人的片子里，你让观众怎么笑？<br />
<br />
　　一票难求　风景不再<br />
<br />
　　张艺谋之前的大片上映时，一般都是一票难求。2006年的《满城尽带黄金甲》，北京很多影院出现头天排队买第二天票的情况。不过这次的《三枪》的销售就不容乐观了。<br />
<br />
　　《三枪》是昨天下午6点上映的，昨日下午4点记者致电重庆各大影院时，预售票情况都非常一般，多的也仅有几十张，当时，各个影院首场票都还没有卖完。对于这种情况，中影今典王小姐称，可能是现在观众变得理智了。<br />
<br />
　　拷贝数多　票房难说<br />
<br />
　　《三枪》票房号称要一枪一个亿，但昨天影片终于示人后，这个目标值得怀疑。<br />
<br />
　　一个影片的票房取决于宣传和口碑，但记者从一些影院人士处了解到，他们对《三枪》的票房不像片方那么乐观，一是担心影片的口碑，二是影片中东北二人转的东西，可能不会对南方观众的口味。至于拷贝方面，《三枪》在重庆投放了20多个，仅次于《建国大业》，但要达到《建国大业》4亿多的票房，基本是无法完成的任务。<br />
<br />
　　观众声音<br />
<br />
　　●如果此片票房过亿，那只能说是国人对电影审美上有极大的缺陷。但愿票房惨淡，以便让我们对中国电影还抱有美好的幻想。<br />
<br />
　　●孙红雷、闫妮，别听着张艺谋的名气就张牙舞爪地献身了，你们跟小沈阳不是一类的呀！这哪叫喜剧，大街上看神经病的本色演出也比这强啊！<br />
<br />
　　●张导早已是投机商了，时刻把握时代的趣味，一部部所谓的大片出现。《三枪》的票房或许不会差，但也掩盖不了张导的苍白。<br />
