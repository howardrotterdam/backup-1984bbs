													<h2>逼上梁山了―― 黄健翔的愤怒</h2>
						
						
																											<div id="postmessage_751290" class="t_msgfont">这是今年春节期间冬奥会比赛后写的一篇。因为不想惹事，自我和谐了。今天，不是看热闹的不怕麻烦大，是被逼到墙角了的人没办法只好捅破一切。不是还有人振振有词地坚持说是我当初的所谓“解说门”事件给国家惹了外交纠纷吗？逼着我说出来，那就说呗。大家先看着，我还有话说呢，比如，当初我的“道歉”是怎么回事……不让我过，就都别过了。<br />
<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp; 昨天忙了一天，没有就冬奥会上我国短道速滑接力击败X国夺金在围脖里说话，居然有朋友电话来催我说点儿什么。呵呵，荣幸。今天写一个完整的放在博客里。<br />
<br />
&nbsp; &nbsp; 为什么大家这么高兴？因为我们战胜的对手在各体育赛场上一贯不择手段举世闻名。更有甚者，2007年亚冬会她们战胜本土作战的我队后，居然当着满场中国观众打出一标语：白头山（长白山）是大XX国的领土。对了，还有端午节，还有中药，还有孔子，还有曹操，还有还有……<br />
<br />
&nbsp; &nbsp; 2002世界杯X国打意大利是我解说，平静接受因为一切早在意料之中；后来打西班牙，主持人建宏忍无可忍面对黑哨怒批：这不是亚洲足球的光荣！第二天人家驻华使馆提交抗议，我外交部居然迅速当作大事给转来广电总局了，结果建宏被批。我是外交学院毕业，根据我的相关知识，还真不知道这事也归使领馆负责，好像不记得我国使领馆因为国外的解说采取过什么动作，因为那样做太掉价，没有大国风范。后来通知我们解说和评论不许再谈裁判问题。动用外交渠道抗议他国体育解说，举世罕见；真的竟然受理的恐怕也只有宽宏大量克己复礼的我们了。<br />
<br />
&nbsp; &nbsp; 最可笑的在后头：世界杯结束后录制聊天节目里，一向稳当的白岩松居然忘了纪律提起裁判问题，我赶紧接话说不是能力问题，看后来比赛都挺好，这个话题就此打住。不想播出时恰好被某巨官看到我说的话，立即电话时任台长点名怒批我“不服管理”。真是别人偷驴我拔橛。台长震怒，狠批体育频道负责人，我也从此上了黑名单。不知那位当时点名的高官现在何处，还记得此事否？现在想想您至于的吗？关键是我说的这段话，棒子都没投诉呢。直到今天，我也不知道到底是哪一位、多大的官，为了一句关于足球裁判的话生那么大气发那么大火儿，连累我们台长都被　Ｎ尬⒉恢良嫱武啊。<br />
<br />
&nbsp; &nbsp; 过去读书说晚清落末，于是“百姓怕官，官怕洋人，洋人怕百姓”。我就纳闷了，怕洋人也罢了，连棒子也怕，还不如李鸿章袁世凯呢。对我们一小老百姓抖起威风来那叫一个雷霆万钧。国内的哈X风气，这样的官员多少负有责任。<br />
<br />
&nbsp; &nbsp; 还有呢。2004亚洲杯在我国举办，建宏不幸又解说了一场X国的比赛，结果人家大使馆又一次抗议过来，说建宏的言语里有不尊重他们运动员的地方。那场球我从头听到尾，解说没有一点儿毛病，只是因为他们被伊朗队给灭了，找地方出气而已。外交部又接受了他们的抗议并转交“有关部门”：他们对待外国人无理取闹的态度，比信访办对待上访群众的态度真是好多了。<br />
<br />
&nbsp; &nbsp; 那以后我就知道，原来X国驻华使馆还兼职我国广电部门监听监看小组成员呢。<br />
<br />
&nbsp; &nbsp; 2006年又是世界杯了，希丁克带某队遭遇意大利，全世界媒体的话题都是意大利队“复仇”，唯独我们被告知不许提4年前的事。甚至连什么“报仇”、“雪恨”、“某国”、“四年前”都不许说，仿佛四年前的比赛不存在。<br />
<br />
&nbsp; &nbsp; 这难不倒我。我只字不提他们和四年前，最后照样把气出了。只有健忘的假球迷想不明白而已。<br />
<br />
&nbsp; &nbsp; 所谓又“惹祸”了。“惹祸”怎么了？惹完还不是得我来解说两场1/4决赛、半决赛和最后的决赛？This is it。就是这样。当然，上述这些不是“出事”唯一的原因，这么大一件事，总归是很多事综合作用发酵而成的。但是，这确实是重要因素之一。<br />
<br />
&nbsp; &nbsp; 在这里必须补充说明的是：我们某些国际主义精神很强的同胞兼同行，在那场解说风波后不断电话去澳大利亚大使馆提醒人家，你们怎么不通过外交渠道抗议黄健翔的解说？<br />
<br />
&nbsp; &nbsp; 结果人家的文化参赞接电话说：我们没觉得他说的有什么问题，没理由动用外交手段；而且我们看球时对希丁克的保守畏缩的谴责比他还强烈呢！最后，这就是一场球赛而已，就算他真的不喜欢澳大利亚队，也没什么，这是他的权力。请你们不要再打电话来了。搞得检举我的同胞十分郁闷，就差骂人家“澳奸”了。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;今天我告诉这样的同胞：你以为人家澳大利亚民主政府的驻外使馆跟你或者X国使馆一样BT和土鳖呢？动用外交渠道抗议一个体育解说，那是侵犯人权，动用国家机器迫害个人，会被整个西方民主世界当作笑料的。人家要是照你说的做了，我下半辈子就可以不用工作，直接去澳大利亚拿着当地政府津贴养老去了。<br />
<br />
&nbsp; &nbsp; 有些事情是躲不开的。今年春节前，中国足球32年来第一次击败X国的3：0 ，由于AV不直播国足比赛，还偏偏就是本人在新浪给全国网友解说的。我一个退役解说都没错过这样的历史时刻，真是天可怜见。这就是天意！<br />
<br />
&nbsp; &nbsp; 我当初大学毕业时宁可北漂也不去外交部。假如我在，又赶上某国外交渠道抗议我国电视台的足球解说，我会这么回答：我们认为我国解说员的解说与评论是公正客观准确的。而且，一个体育解说不可能是代表政府去评球的，比如他说：这个球踢的真离谱啊――难道也是代表本国政府？这政府也太累了。照此标准要求，西方电视台的解说员非得害得他们的女王或者总统、首相和总理们天天道歉不可。最后，因为国际公约里没有涉及体育解说的相关条款，这不是新闻、社论，建议你们以球迷身份尝试与他交流，不要走外交渠道。我们可以提供帮助联系到他本人并提供场所。同时会有批量中立的中国球迷及对方球迷（意大利、西班牙乃至伊朗的）到场，以保证交流的公平友好环境。――大家觉得我这样答复是不是很“外交”啊？<br />
<br />
&nbsp; &nbsp; 最新可靠消息是：这次冬奥会我短道速滑接力战胜她们夺取金牌后，该国使馆又来抗议了：你们不许说自己是战胜我大XX国队夺金的！不是你们战胜的我们，是裁判！你们不许说打破我们18年的垄断，你们只赢了一次，等你们赢18年再说打破！<br />
<br />
&nbsp; &nbsp; 抗议有效。外交渠道转达给广电系统，传达到冬奥报道前方，准备好的系列庆祝节目都被拿下，语言措辞一审再审，千万不要让友邦不开心……<br />
