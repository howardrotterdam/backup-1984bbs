工会力量的来源<br />
　　工会为什么能够提高其会员的工资，工会力量的基本来源是什么？回答是：工会可以缩减可得到的就业机会，换言之，可以缩减适于从事某类工作的人数。通常在政府协助下，用实行高工资率的办法，工会得以缩减就业机会。同样在政府协助下，主要是通过发给许可证的方式，工会得以缩减合用者的人数。工会偶尔还同雇主相勾结，对其会员所生产的产品实行垄断，以这种办法来增加其力量。<br />
　　实行高工资率。如果工会可以设法使承包人支付给水管工或木匠的工资不少于，比如说每小时十五美元。那将会减少这方面的就业机会。当然，这也将增加愿意从事这类工作的人数。<br />
　　假定目前可以实行这种高工资率。那么，必须采取某种办法来在寻求这种有利可图的工作的人们中间分配有限的就业机会。已采用过的许多方法包括：搞裙带关系，即把工作保留给家庭成员；按照资历和学历招工；超额雇用工作所需要的人员，即随便安排工作；以及不折不扣的行贿受贿。由于牵扯到很多利害关系，因而采用哪种方法对于工会来说是个很棘手的问题。某些工会不允许在公开会议上讨论有关资历方面的规定，因为这种讨论常常引起殴斗。为了优先获得工作而向工会官员们付酬金，是一种普通的行贿形式。工会采取的种族歧视措施虽然受到了猛烈的抨击，但仍然是分配工作的另一种方法。如果申请工作的人过多，供分配的工作有限，则招工方法必定是武断的。用偏见及类似的不合理的方法来解决把谁关在门外的问题，这种做法常常得到“已入会者”的大力支持。种族和宗教歧视也渗入到了医学院校的入学方面，原因同上面一样，即可接受的申请者过多，因而需要在他们中间进行分配。<br />
　　让我们再来看工资率，工会采用什么方法实行高工资率呢？一种方法是采取暴力行动或以暴力相威胁：宣称如果雇主雇用非工会会员，或付给工会会员的工资低于工会指定的工资率，将毁坏雇主的财产或者殴打他们；宣称如果工人同意为较低的工资干活，就揍他们或破坏他们的财产。这就是工会在进行工资调解和谈判时，为什么经常伴随有暴力行为的原因。<br />
　　一种更为容易的方法是取得政府的帮助。正因为这个缘故，工会都把总部设在华盛顿美国国会的附近，而且在政治活动上花费大量金钱和精力。霍普金斯在有关航空公司驾驶员工会的研究报告中特别提出：“该工会得到了联邦立法的充分保护，使职业航空公司驾驶员实际上成了受国家保护的人。”①<br />
　　<br />
　　①霍普金斯：《航空公司驾驶员》，第2页。<br />
　　政府帮助建筑工人工会的主要形式是戴维斯一培根法案，该联邦法令规定，凡是同联邦政府签订有价值二千美元以上合同的承包人，支付的工资率不得低于有关地区由劳工部长决定的“同等工人和技工普遍享有的”工资率。实际上，“在决定工资的绝大多数场合……不论建筑面积和种类如何，普遍享有的工资率”往往被规定为工会的工资率。①②后来，这一有关工资率的条款写进了其他许多有关联邦政府援建项目的法令，写进了三十五个州（截至1971年）颁布的有关建筑开支的法令，从而扩大了上述法案涉及的范围。②实施这些法令的结果是，政府对于大量建筑活动实行了工会的工资率。<br />
　　<br />
　　①约翰&#8226;P．古尔德：《戴维斯-培根法案》，第15号专题研究（美国首都华盛顿：美国企业研究所，1971年11月），第10页。<br />
　　②同上，第1、5页。<br />
　　甚至使用暴力暗中也包含有政府的支持。一般说来，在劳资争议中公众是同情工会的，这导致了政府当局容忍在其他情况下决不会容忍的行为。在劳资争议中，某人的小汽车被推翻，工厂、商店或住家的窗子被捣毁，甚或有人遭到殴打并严重受伤，肇事者不大可能被罚款，更不用说去坐牢了，但如果在其他情况下发生同样的事情，情形就不一样了。<br />
　　政府实行工资率的另一套措施是最低工资法令。颁布这些法令据说是为了帮助低收入者。其实，它们损害了低收入者。要求颁布最低工资法令的压力，来自那些在国全面前作证主张提高最低工资的人。这些人不是贫苦人民的代表。他们主要是劳联－产联以及其他劳工组织的代表。在这些工会中，没有一个会员挣得的工资接近法定的最低工资。尽管有一套关于帮助穷人的漂亮话，但他们主张提高最低工资实际上是为了保护其工会的会员免受竞争的危害。<br />
　　最低工资法令要求雇主们歧视技术低的人。没有人这么明说过，但事实确实是如此。且以受教育很少而没有什么技能的青少年为例，其劳务比如说每小时仅值二美元。他或她也许渴望为这种工资干活，为的是掌握较多技能，从而得到较好的工作。但最低工资法令宣称，只有雇主愿意付给他或她（在1979年）每小时二点九美元，这样的人才能受雇。也就是说，除非雇主愿意仁慈地把九十美分加到青少年劳务所值的二美元上面，否则他们是不会被雇用的。青年人因不能得到每小时二点九美元而失业，反而说这种境况比接受每小时二美元的工资而就业要强些，这对我们来说一直是不可思议的事情。<br />
