　　限制人数。实行工资率的另一种方法是直接限制可能从事某种职业的人数。当雇主众多，难于实行某种工资率时，这种方法是特别有吸引力的。医疗事业是一个极好的例子，某工会的大量活动是以限制开业医生为目的的。<br />
　　正象实行工资率一样，要想在限制人数方面获得成功，通常需要政府的帮助。在医疗事业中，发放医生开业许可证是关键问题：凡想要“行医”的人必须得到政府的许可。不用说，只有医生有可能被认为有能力判断医生候选人的资格，因此各州（在美国，发放许可证的工作在州政府的管辖权限之内，联邦政府不管此事）签发执照的部门通常由清一色的医生组成，或者医生占优势，这些医生一般又是美国医学协会的会员。<br />
　　上述主管部门或州的立法机关提出了批准许可证的各项具体条件，实际上是让美国医学协会来左右获准开业的人数。条件包括：必须受过长期的训练，必须毕业于某一“得到政府批准”的学校，并在某一“得到政府批准”的医院担任过实习医生。绝非偶然，“得到政府批准”的学校和医院的名单，一般是与美国医学协会的医学教育和医院委员会公布的名单完全相同的。任何学校如不得到美国医学协会的医学教育委员会的批准，是建立不起来的，或即使建立起来，也不能维持很久。得到批准有时需要按照该委员会的意见限制医生人数。<br />
　　在经济压力特别大的三十年代萧条时期，有组织的医学界显示了限制从医人数的巨大力量。尽管当时从德国和奥地利（那时都是先进医学的中心）涌入了大批受过严格训练的难民。但希特勒上台后的五年中，获准在美国开业的外国医生，并不比前五年多。①<br />
　　<br />
　　①参看米尔顿&#8226;弗里德曼和西蒙&#8226;库兹内茨：《独立职业行当的收入》（纽约：国家经济研究局，1945年），第8－21页。<br />
　　营业执照的发给被广泛用来限制各种职业的从业人数，特别是医学等职业的从业人数，在这类职业中有许多单个的开业医生，他们同大量的个别主顾打交道。象在医学中一样，主管发放执照的部门，主要是由该行业持有营业执照的成员组成――不论他们是牙医、律师、整容专家、航空公司驾驶员、水管工，还是殡仪业者。没有哪个职业如此冷僻，以致无须用发给执照的办法来限制从业人数。据联邦贸易委员会主席说：“在某州议会最近召开的一次会议上，各职业集团纷纷要求发给执照，其中有拍卖人、采矿者、房屋改建承包商、宠物饲养人、电学家、性病医生和性生活顾问、数据处理者、估价人以及电视机修理人。夏威夷州签发文身艺术家执照。新罕布什尔州签发避雷针推销员执照。”①签发各种执照的理由总是一样的：保护消费者。然而，要发现真正的理由，我们得看一看在各州议会里是谁在为实行或巩固营业执照制度进行游说。游说者一律是有关从业人员的代表，而不是顾主的代表。千真万确，水管工也许比其他任何人更清楚地知道如何保护他们的顾客。然而，当水管工在背后竭力谋取合法权力以决定谁可以当水管工时，就难以把他们对顾客的利他主义的关心看成是主要动机了。<br />
　　加强对本行业就业人数的限制，同时增加领有执照的开业者的业务，各有组织的职业集团总是千方百计使其业务活动范围规定得尽量宽一些。<br />
　　<br />
　　①迈克尔&#8226;佩茨丘克：《需要和收入》，载《管理》杂志，1979年3月/4月。实施脊柱按摩疗法和整骨术。<br />
　　通过签发执照限制从事各种职业的人数，其结果之一是创造出了一些新学科：例如在医学中出现了整骨术及脊柱按摩疗法，这些医疗科目试图采取发给执照的办法，限制其人数。美国医学协会已提出许多诉讼，指控一些脊柱按摩医生及整骨医生非法开展其他医疗业务活动，企图把他们限制在尽可能狭小的营业范围之内。而脊柱按摩医生和整骨医生则控告其他医生没有得到执照就实施脊柱按摩疗法和整骨术。部分地由于新的高度精密的手提设备的出现，最近在各类居民区中发展起一种新的保健服务项目，即在紧急情况下，可以提供急救服务。这类服务有时由市政府或市政府的一个机构提供，有时由不折不扣的私人企业提供，这类机构的人员主要是医务辅助人员，而不是持有执照的医生。<br />
　　加利福尼亚州南部一个附属于城市消防队的上述私人企业的老板乔&#8226;多尔芬，描述该企业的效能如下：<br />
　　在我们服务的加利福尼亚州的一个地区，这是一个拥有五十八万人口的县，在提供急救服务以前，因心脏病突然发作而心脏停止跳动的病人，通过住进医院而活过来并且病愈出院的不到１％。提供急救服务以后，仅在头六个月中，23％的心脏停止跳动的病人被成功地救活了，他们病愈出院并且回到了社会生产岗位上。<br />
　　我们认为这是很了不起的，事实说明了一切。然而，说这同医学界有关系有时是很困难的。他们有他们自己的想法。<br />
　　更为一般地说，导致工人停工一个最经常的原因，是所谓管辖范围争议，即有关各职业业务活动范围的争议。曾采访过我们的一个广播电台记者，就是一个有趣的例子。他强调采访要短，以便在他的盒式录音机的录音带上只录一面。翻录音带的工作必须留给电工工会的一个会员去做。他说，如果他自己把它翻过来，当他回到广播电台时，录了音的磁带会被洗掉，采访就白费了。这种行为正同医生反对辅助医务人员提供急救服务一样，并且其动机也是一样的：增加对一个特殊集团的服务的需求。<br />
　　工会和雇主的勾结。工会有时通过帮助工商企业联合起来规定价格或者分配市场来增强自身的力量，而规定价格或者分配市场等活动在反托拉斯法令条件下对工商企业说来是违法的。<br />
　　从历史上说，三十年代的采煤业是这方面最重要的事例。当时的两个格费伊煤炭法案试图为煤矿经营者共同规定价格的行为提供法律上的支持。当三十年代中期第一个法案被宣布为违反宪法的法案时，约翰&#8226;Ｌ&#8226;刘易斯以及他所领导的美国矿工联合会挺身担负起了重担。每当开采出来的煤炭量多到似乎将迫使价格下跌时，刘易斯就与采煤工业进行默契般的合作，通过号召罢工或停工，控制产量并从而控制价格。正如一个煤业公司的副董事长在1938年指出的：“他们（美国矿工联合会）已做了大量事情来稳定烟煤工业，并尽力使该工业能够继续盈利。虽然他们不愿承认这一点，但实际上他们按上述方式所作的努力……比起煤矿经营者自己的努力……要稍许更加有效一些。”①<br />
　　<br />
　　①瓦利&#8226;坎普煤炭公司执行副经理威廉&#8226;泰勒，引自梅尔文&#8226;杜波斯基和沃伦&#8226;范‘蒂恩的《约翰&#8226;L．刘易斯传》（纽约：方形丛书和纽约时报图书公司，1977年），第377页。<br />
　　利益在经营者和矿工之间被瓜分了。矿工得到了高工资，这自然意味着更多的机械化和较少的矿工被雇用。刘易斯明确承认这种结果，并且更愿意接受这种结果――他把受雇的矿工的高工资看成是对受雇人数减少的一种充分补偿，假如受雇者都是该联合会会员的话。<br />
　　矿工联合会之所以可以起这种作用，是因为工会不受谢尔曼反托拉斯法的限制。利用这一便利条件从事非法活动的工会，与其说是工人组织，倒不如说是出售使整个行业卡特尔化的劳务的企业。卡车司机联合会也许是最受人注意的工会。关于詹姆斯&#8226;霍法以前的卡车司机联合会主席戴维&#8226;贝克（两人都已入狱），有一个或许不足凭信的故事。当贝克与华盛顿州的各啤酒厂就啤酒厂卡车司机的工资进行谈判时，他被告知他所要求的工资是不能实现的，因为“东部啤酒”的价格会因此而低于华盛顿州的啤酒价格。他问东部的啤酒应该是什么价格，才能接受他要求的工资。有人回答说，东部啤酒每箱的价格应该是Ｘ美元。据说他当时许下愿：“从今后东部啤酒每箱的价格保准会变成Ｘ美元。”<br />
　　工会可以而且的确经常为其会员提供有用的服务，例如：就会员的就业条件同雇主进行谈判，反映会员的疾苦，以及使会员感觉到有所依附，有所作为。作为自由的信仰者，我们赞成给予人们最充分的机会，自愿组织工会，并且认为工会可以提供会员所希望并愿意为此出钱的任何服务，只要工会尊重其他人的权利而且不使用暴力。<br />
　　然而，工会以及职业协会等组织，并没有依靠严格自愿的活动和全体会员来达到其公开宣布的主要目标，即提高会员的工资。工会及类似的组织在使政府给予它们特权与豁免权方面取得了成功，这些特别权利使他们在牺牲其他工人和全体消费者利益的条件下，得以让它们的某些会员和官员受益。总的说来，受益者的收入要比受害者的收入高许多。<br />
　　　<br />
政府<br />
　　除了保护工会会员之外，政府还通过了旨在一般地保护工人的大批法令：向工人提供补偿费的法令，禁止雇用童工的法令，规定最低工资和最高工时的法令，建立保证实行公平就业的各种委员会的法令，促进积极行为的法令，建立调节就业的联邦安全和卫生管理局的法令，以及其他不胜枚举的法令。<br />
　　某些措施对工作条件的改善产生了有利的影响。大多数法令，如工人补偿费法令和童工法，在它们颁布以前，就已体现在人们的自觉行动中了，颁布这些法令也许只是对边远地区具有实际意义。其他法令，你知道后不要感到惊奇，既带来了好处，也带来了害处。它们在减少普通工人的就业机会和收入的同时，却为某些工会和雇主提供了权力的来源，为官僚们提供了官职的来源。安全和卫生管理局是一个最好的例子――这是一个可怕的官僚主义机构，人们已对它怨声载道。正如最近流传的一个笑话所说：一个人问另一个人：你知道需要多少美国人安一个电灯泡吗？另一个人回答说：需要五个人，一个人安灯泡，四个人填写环境影响报单以及安全和卫生管理局要求的各种报告书。<br />
　　政府确实在很好地保护一类工人，即由政府雇用的工人。<br />
　　马里兰州的蒙特戈梅里县，离美国首都华盛顿半小时的路程，是许多高级文职人员的住宅区。在美国所有的县份当中，该县是家庭平均收入最高的。蒙特戈梅里县每四个就业人员中，就有一个是为联邦政府工作的。他们不担心失业，有同生活费用指数相联系的薪金。退休后，他们享有文职人员养老金，这种养老金也同生活费用指数相联系，并且不依赖于社会保险。许多人又极力取得领取社会保险金的资格，成了所谓拿双份养老金的人。<br />
　　上述高级文职人员在蒙特戈梅里县的许多邻人，或许是大多数邻人，作为国会议员、院外活动人员、同政府订有合同的公司的董事长，也与联邦政府有某种联系。同华盛顿周围的其他住宅区一样，蒙特戈梅里县的发展是迅速的。最近几十年中，政府已变成了一种十分可靠而且发展很快的行业。<br />
　　所有文职人员，甚至低级文职人员，都受到了政府的良好保护。根据大多数的研究报告，他们的平均薪金要高于同等私人企业雇员的薪金，并且不受通货膨胀的影响。他们享有大量的额外优惠，而且具有一种令人难以置信的职业安全感。<br />
　　正如《华尔街日报》的一篇报道所说：<br />
　　当（文职人员）管理规章激增，录满二十一大本，堆起来高达大约五英尺的时候，政府主管人员感到越来越难于解雇雇员了。与此同时，提级和增加工资则成了自然而然的事情。其结果是产生了一种几乎没有刺激、不受任何人控制的官僚统治机构。……<br />
　　去年符合增加工资条件的一百万人当中，只有六百人的工资没有增加。几乎没有一个人被解雇；去年丢掉工作的联邦工作人员不到1％。①<br />
　　<br />
　　①卡伦.埃利奥特.豪斯：《推不动的司局：文官条例可能埋葬卡特提高效率的努力》，载《华尔街日报》，1977年9月26日，第1页，第1栏。<br />
　　让我们来看一个具体事例：1975年1月，环境保护局一名打字员上班一贯迟到，以致他的上司要求将他解雇。这件事办了十九个月。如果把记录有关步骤的纸张连在一起，足有二十一英尺长。为了满足所有规章制度方面的要求，满足所有劳资协议上的要求，缺少哪一步骤也不行。<br />
　　卷入这一过程中的有这个雇员的顶头上司、局长和副局长、人事处长、局内该部门的主管人、两名雇员关系专家、专门的调研办公室以及该办公室主任。不用说，这一大串官员为此而做工作是用纳税人的钱来支付的。<br />
　　在州和地方政府各级，情况是各不相同的。在许多州以及象纽约、芝加哥和旧金山等大城市中，情况或者同联邦政府相类似，或者比联邦政府还要糟糕。纽约市落到它目前实际上的破产状态，主要是由于市政雇员工资的急剧增长，也许，更为重要的原因是对提早退休人员给与优厚的养老金。在拥有大城市的各州，政府雇员的代表常常是州立法机关中的主要特殊利益集团。<br />
　　　<br />
没人提供保护<br />
　　以下两种工人是得不到任何人保护的：一种是仅有一个可能的雇主的工人，另一种是没有任何可能的雇主的工人。<br />
　　那些实际上只有一个可能的雇主的人，往往可以得到很高的报酬，因为他们的技能实在罕见，价值极高，只有一个雇主能够加以充分利用。<br />
　　我们三十年代学习经济学时，教科书中的标准例子是棒球大王巴比&#8226;鲁思。他作为棒球之王译名叫做“最佳击球手”，他是那个时代最最受人欢迎的棒球选手。当时有两个主要的棒球俱乐部，不论鲁思为它们中的哪一个打球，都能使运动场卖满座。纽约扬基俱乐部恰巧拥有最大的运动场，因而它可以比任何其他俱乐部付给鲁思更多的钱。结果，扬基俱乐部实际上成了他唯一可能的雇主。当然，这并不意味着巴比&#8226;鲁思不能获得高薪金，但确实意味着没有人保护他；他不得不同扬基俱乐部讲价钱，把不为他们打球作为他进行威胁的唯一武器来使用。<br />
　　没有可能选择雇主的人大都是政府措施的受害者。其中一类人我们已经提到过，即那些由于法定最低工资而找不到工作的人。正象前面已经指出的，他们中间许多人是政府措施的双重受害者：低劣的教育加上高额最低工资，后者阻碍了他们获得在职训练。<br />
　　依靠政府救济的人多少处于相似的境地。只有当他们能挣到的收入足以补偿救济金或其他政府补助的时候，就业才对他们有利。但他们的劳务也许对于任何雇主都没有那么高的价值。七十二岁以下的依赖社会保险津贴生活的人，情况也是这样。如果他们可以挣得超过某一限额的收入，他们就会失掉其社会保险津贴。这就是近几十年超过六十五岁而仍然工作的人所占的百分比，为什么急剧下降的主要原因，对男性来说，这一比重由1950年的45％下降为1977年的20％。<br />
　　　<br />
其他的雇主<br />
　　许多雇主的存在，可以为大多数工人提供最可靠而且最有效的保护。上面我们已经看到，只有一个可能的雇主的人，几乎或者根本得不到保护。保护工人的是那些愿意雇用工人的雇主。雇主对工人劳务的需求，使得他们完全是为了自身的利益而向工人支付其工作的全部价值。如果某一雇主不愿交付，另一雇主会支付。真正保护工人的，正是这种争夺工人劳务的竞争。<br />
　　当然，其他雇主的竞争有时激烈，有时不激烈。一些机会会相互冲突，而另一些机会却不被人们所知。雇主要找到理想的受雇者，受雇者要找到理想的雇主，都要付出很高的代价。我们的世界并非尽善尽美的世界，因而竞争不能提供十全十美的保护。然而对于绝大多数工人说来，竞争是迄今被人们所发现或发明的最好的，也即害处最少的保护。<br />
　　竞争的这种作用是我们一再提到的自由市场一种特性。其他雇主的存在保护了工人免受其雇主的剥削，因为他可以到别处干活。其他工人的存在保护了雇主免受工人的剥削，因为他可以雇用别人。其他卖主的存在保护了消费者免受某一卖主的剥削，因为消费者可以到别的商店买东西。<br />
　　我们的邮政服务质量为什么低劣，我们的长途火车服务质量为什么低劣，我们的中小学教育质量为什么低劣，都是一个原因造成的，就是我们实际上只能从一处得到上述服务。<br />
　　　<br />
结论<br />
　　当工会用限制人们从事某种职业的办法来为其会员获取较高工资时，这种较高工资是通过损害其他工人的利益获得的，这些工人发现他们的就业机会减少了。当政府向其雇员支付较高工资时，这种较高工资是通过损害纳税人的利益获得的。但是，当工人们通过自由市场获得较高工资和较好工作条件时，当工人的工资由于各厂商为得到最好的工人彼此进行竞争，由于工人们为得到最好的工作彼此进行竞争而增加时，这种较高工资不损害任何人的利益。这种较高工资只能来自较高的生产率、较大的资本投资以及更加广泛推广的技能。整个馅饼是增大了――不仅工人得到的份额增大，而且雇主、投资者、消费者乃至税收官员得到的份额也增大了。<br />
　　这就是自由市场制度在全体人民中间分配经济进步的果实的方式。这就是过去两个世纪劳动人民的生活和工作条件得到巨大改善的秘密所在。<br />
