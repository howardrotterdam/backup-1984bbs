谁得到了好处？<br />
　　医生在美国属于享有最高报酬的劳动者。这种状况对于已经从工会得到好处的人们说来并不是例外情况。尽管人们经常得到的印象是，工会保护低工资工人免受雇主的剥削，但实际上不一定是这样。最成功的工会总是这样一些工会，其会员从事的职业需要熟练的技能，不论有没有工会，他们的工资都比较高。这类工会只是使本来已经很高的工资更高。<br />
　　例如在美国，航空公司驾驶员每周工作三天，1976年他们的平均年薪是五万美元，而且这以后又有相当大的提高。在一份题为“航空公司驾驶员”的研究报告中，乔治&#8226;霍普金斯写道：“今天飞机驾驶员惊人的高薪金，与其说来自他们承担的责任或他们掌握的技术，不如说来自他们通过工会获得的受到保护的地位。”①<br />
　　<br />
　　①①乔治&#8226;E.霍普金斯：《航空公司驾驶员：对杰出人物工会的研究》（坎布里奇：哈佛大学出版社，1971年），第1页。<br />
　　在美国，最老的传统工会是行业工会，如木匠、水管工、泥水匠等工人组织的工会，他们同医生一样，也是技术熟练、工资较高的工人。最近，发展最快的工会――而且的确几乎是唯一有所发展的工会――是政府工作人员（其中包括中小学教师、警察、卫生工作者以及其他各种政府雇员）组织的工会。纽约市的市政工人工会，通过把该城市推到破产的边缘显示了它们的力量。<br />
　　中小学教师和市政雇员的情况，说明了一条在英国已经清楚地得到了证明的一般原理。他们的工会不直接同支付他们会员薪金的纳税人打交道，而同政府官员打交道。纳税人和工会与之打交道的政府官员之间的关系愈松弛，政府官员和工会相互勾结牺牲纳税人利益的趋势就愈益加强――这是某些人把另一些人的钱用之于第三者身上的另一个例子。这就是为什么纽约等大城市中的市政工人工会比小城市中的市政工人工会强大的原因，同时也是随着政府对学校活动和教育经费的控制日益集中化，日益脱离地方政府，中小学教师的工会越来越强大的原因。<br />
　　同美国相比，英国政府对更多的工业部门实行了国有化，其中包括煤炭工业、公用事业、电话和医院。在英国的国有化工业部门中，工会一般特别强大，劳资问题也最为严重。同样的因果关系也反映在美国邮政工人工会的力量中。<br />
　　假定强大的工会的会员工资比较高，明显的问题就是：是因为工会强大，会员们才得到高工资呢；还是因为会员的工资高，工会才强大，为工会辩护的人宣称，会员的高工资可以加强工会组织的力量，而且，一当全体工人都是工会会员时，所有工人都将领取高工资。<br />
　　然而情况是复杂得多的。极为熟练的工人的工会无疑地能提高其会员的工资；可是，不论怎样都会得到高报酬的人，在组织强有力的工会方面是处于有利地位的。而且，工会提高某些工人工资的能力，并不意味着普遍实行工会制度会提高全体工人的工资。正好相反，这是产生误解的根本原因，强大的工会能为其会员赢得利益，首先是靠牺牲其他工人的利益。<br />
　　理解这一点的关键是了解经济学的最基本的原理，即需求法则：某种东西的价格愈高，愿意购买它的人就愈少。使某种劳动较为昂贵，这种劳动提供的工作机会就会减少。使木匠活较为昂贵，则建造的房屋减少，并且所造的这些房屋会采用木匠活不多的建筑材料和方法。提高航空公司驾驶员的工资，乘飞机旅行将变得更贵。乘飞机的人将会减少，因此对于航空公司驾驶员来说就业机会也会少一些。反过来说，减少木匠、飞机驾驶员的人数，他们就会得到较高的工资。缩减医生的人数，他们就能收取较高的酬金。<br />
　　一个成功的工会可以减少它所控制的工作机会。其结果是，希望按照工会的工资标准获得这类工作的某些人，就不会达到目的了。他们被迫转向别处。更多的工人会寻找其他工作，压低了这些工作的工资。普遍组织工会不会改变这种局面。这对于找到职业的人意味着高工资，与此同时，对于其他人则意味着更多的失业。更为可能的是，会出现一些强大的工会和弱小的工会，强大工会的会员会象现在这样，在损害弱小工会会员利益的情况下得到较高工资。<br />
　　工会领导人经常说可以通过减少利润来提高工资。这是不可能的：根本没有富余的利润来提高工资。美国的全部国民收入目前约有80％用于支付工资、薪金和小额优惠。余额的一半以上用于支付租金和贷款的利息。公司利润――这是工会领导人常常提到的――总额不到国民收入的10％。这还是纳税前的利润。纳税以后，公司利润大约是国民收入的６％。即使全部利润都投放进去，也几乎不可能使所有人都领高工资。而且，这不啻于杀鸡取蛋。最低限度的利润，为投资于工厂和机器以及发展新的产品和新的方法，提供了刺激。这种投资和这些革新，近几年来提高了工人的生产率，并为高而又高的工资准备了必要的资力。<br />
　　提高某些工人的工资必然损害其他工人。将近三十年以前，我们当中有人曾经估计，在我国平均约有10－15％的工人，通过工会或者类似美国医学协会这样的组织，得以使他们的工资比在没有工会的情况下多提高了10－15％，而另外85―90％的工人挣得的工资则因此而减少了大约４％。最近的研究表明，现在的情况大体上仍然是这样。①高工资的工人工资越来越高，低工资的工人工资越来越低。<br />
　　<br />
　　①①米尔顿&#8226;弗里德曼：《论工会对经济政策的影响》，见大卫&#8226;麦科德&#8226;赖特编的《工会的影响》（纽约：哈考特&#8226;布雷斯公司，1951年），策204-234页。十多年以后，H．G．刘易斯经过更详细和更广泛的研究，得出了同样的估计，见其著作《美国工会主义和比较工资》（芝加哥：乏加哥大学出版社，1963年），第5页。<br />
　　我们所有的人，包括由工会高度组织起来的人们，作为消费者，由于工会会员的高工资对消费品价格的影响而间接地受到损害。对于包括木匠在内的每一个人说来，房屋的售价太高了。工会阻挠工人运用他们的技能生产价值最高的东西，工人被迫从事那些生产率较低的活路。对于我们全体说来，可得到的物品的总量，比应有的数量要少。<br />
