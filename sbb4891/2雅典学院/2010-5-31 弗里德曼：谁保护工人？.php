　　青少年特别是黑人青少年的很高的失业率，既是一种耻辱，又是社会动乱的一个严重根源。青少年失业率的增加主要是最低工资法令造成的。第二次世界大战结束时，最低工资是每小时四十美分。战时的通货膨胀曾使这个数目低到如此程度，以致它实际上几乎没有什么价值可言。此后最低工资急剧上升，1950年升至七十五美分，1956年上升到一美元。五十年代初期，全体工人的失业率大约为４％，青少年的失业率平均为10％――对于刚加入劳动大军的人们说来，10％的失业率也许比人们预料的稍微高了一些。白人和黑人青少年的失业率大致相等。在最低工资率急剧提高之后，白人和黑人青少年的失业率扶摇直上。意义更为重大的是，在白人和黑人青少年的失业率之间出现了差距。目前，白人青少年的失业率在15－20％之间，黑人青少年的失业率在35－45％之间。①我们认为，在所有法令中，最低工资率法令是最歧视黑人的一项法令。政府先是开办中小学，其中许多青年人，特别是黑人青年，所受的教育很差，以致他们未能掌握必要的技能从事工资较高的工作。随后政府再一次惩罚了他们，阻止他们为了得到在职训练而为低工资干活。所有这一切都是在帮助穷人的名义下进行的。<br />
