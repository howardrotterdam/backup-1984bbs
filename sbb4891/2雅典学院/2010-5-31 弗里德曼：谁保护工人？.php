													<h2>弗里德曼：谁保护工人？</h2>
						
						
																											<div id="postmessage_649943" class="t_msgfont">谁保护工人？<br />
　　米尔顿&#8226;弗里德曼，罗斯&#8226;弗里德曼 <br />
 <br />
　　过去两个世纪里，在美国和其他经济上先进的社会里，普通工人的状况有了极大改善。今天，在上述社会里，几乎没有任何工人从事那种繁重不堪的劳动，这种劳动在大约一个世纪以前是常见的，而在地球上大部分地区现在仍然是不足为奇的。工作条件得到了改善；工作时间也缩短了；享受假期和其他小额优惠成了理所当然的事。收入大大增加，使普通家庭得以享有早些时候只有少数富人才能享有的生活水平。<br />
　　如果进行一次盖洛普民意测验，向人们提出这样的问题：“促使全体工人状况得到改善的原因是什么？”多数人的回答很可能是“工会”，其次是“政府”――尽管作出“没有任何原因”或“不知道”或“没看法”的回答的人也许比前两种人要多。可是，美国和其他西方国家过去两个世纪的历史证明，这些回答都是错误的。<br />
　　在大部分时期中，工会在美国并不重要。直到1900年，全体工人中只有３％是工会会员。甚至在今天，工会会员在工人总数中所占的比例仍然不到四分之一。很显然，工会不是促使美国全体工人的状况得到改善的主要原因。<br />
　　同样，在“新政”以前，政府特别是中央政府对经济活动的控制和干预是最低限度的。政府所起的必不可少的作用，是保护自由市场的正常活动。但是，很显然，直接的政府行动不是促使全体工人的状况得到改善的原因。<br />
　　至于有人说工人状况的改善“没有任何”原因，当前工人的状况正好证明这样回答是错误的。<br />
　　　<br />
工会<br />
　　滥用语言的最惊人的事例之一是把“劳方”看作是“工会”的同义词――例如在新闻报道中，我们常看到这样的词句：“劳方反对”某某提案，或“劳方”的提案如何如何。这是双重的错误。首先，在美国四分之三以上的工人不是工会会员。甚至在英国，那里的工会长期以来远比美国的工会强大，大部分工人也不是工会会员。其次，把“工会”的利益及其成员的利益等同起来是错误的。虽然在大部分时间里，多数工会与其成员的利益是有联系的，而且联系密切，但有许多事例表明，工会头头常常利用合法手段或非法手段如滥用和私吞工会基金，牺牲工会会员的利益而为自己谋私利。这警告我们不要不自觉地把“工会”的利益同“工会会员”的利益等同起来，更不用说把工人整体的利益与工会的利益等同起来。<br />
　　语言的这种滥用现象是由于过高估计工会的影响和作用而产生的。工会的行动是看得见的，并且是有新闻价值的。它们常常成为报纸的头版头条新闻，晚间的电视节目也常常不加删节地加以报道。而决定美国大多数工人工资的“市场上的讨价还价”（亚当&#8226;斯密语），则不那么容易被人看到，也不大引起注意，其结果是讨价还价的重要性被大大低估了。<br />
　　语言的上述滥用也导致了这样的信念，即工会是现代工业发展的产物。事情决非如此。实际上，工会的发展可以追溯到工业革命以前的时期，追溯到封建时期城市和城邦内商人和手工业者的特有组织形式，即行会。的确，现代工会的发展可以追溯到更为遥远的年代，追溯到大约二千五百年前希腊的医生之间达成的协议。<br />
　　公认的现代医学之父希波克拉底，于公元前460年左右出生在希腊的科斯岛上，该岛距离小亚细亚海岸只有几英里之遥。当时，科斯岛是一个繁荣的岛屿，已经是医学中心。在科斯岛上研究医学之后，希波克拉底邀游远方，他作为医生，特别是因为他消灭瘟疫和流行病的本事，逐步树立了崇高的声望。过了一段时间后，他回到科斯岛，在该地他创立并主管了一所医学院和一个医疗中心。他教导所有希望学习的人――只要他们付学费。他的医疗中心在希腊全境出了名，吸引着来自四面八方的学生、病人和医生们。<br />
　　当希波克拉底于一百零四岁（这完全是根据传说）去世时，科斯岛到处都是行医的人，有的是他的学生，有的是他的门徒。争夺病人的竞争是激烈的、因而毫不奇怪，必须采取一致的行动来解决有关竞争的问题――用现代术语来说，就是使医生的行为“合理化”，以便消灭“不公平的竞争”。<br />
　　所以，在希波克拉底去世大约二十年以后（这也是根据传说），医生们聚集到一起创立了行为守则。他们以自己老师的名字给守则命名为希波克拉底誓约。此后，在科斯岛上并日益遍及世界其他地方，每一个新培训的医生，在他开业之前，都要宣誓忠于上述誓约。这个习惯一直延续到今天，在美国已成为大部分医学院校毕业典礼的一部分。<br />
　　象大多数职业法规、商业交易协定和工会合同一样，希波克拉底誓约充满了帮助病人的美好理想：“我将用自己的力量，尽自己最大的能力和判断力解除病人的痛苦。……无论来到哪一家，我都要帮助病人解除痛苦，而决不存心做损害或伤害人的事情。……”等等。<br />
　　但是，该誓约也包含着一些同上述精神不相符的内容。例如誓约上有这样的话：“我将把医术、讲稿和所有其它学问传授给我的儿子、我师傅的儿子以及受过正式训练并宣过誓的人，而不传给别人。”今天，我们也许会把这称作封闭式雇用制度（即只雇用某一工会会员的制度）的前身。<br />
　　誓约还提到了患有肾结石或膀胱结石的病人，原话是这样的：“哪怕是结石病，我也决不动手术，我将交给具有这种技能的医生来处理此事。”①这可以说是内科医生与外科医生划分市场的极好协议。<br />
　　<br />
　　①这誓言有各种译文，本文根据约翰&#8226;查德威克和W.N.曼的《希波克拉底的医学著作》（牛津：布莱克威尔公司，1950年），第9页。<br />
　　我们猜想，当医学院的毕业班宣誓时，希波克拉底在九泉之下定然不得安宁。当年，他曾经把知识传授给每一个对医学感兴趣并且愿意交付学费的人。他可能会强烈反对那种划分市场的做法。自从制定出希波克拉底誓约到今天，全世界的医生为了保护自己免受竞争的危害，一直采用了这种做法。<br />
　　美国医学协会很少被看作是工会，人们认为它的作用远远超出了普通工会的范围。它向其会员及整个医学界都提供重要的服务。然而，它实际上是工会，而且我们认为，是我国最成功的工会之一。几十年来，它缩减了医生人数，抬高了医疗费用，同时阻止了来自外行业的人同“受过正式训练并宣过誓的”医生们相竞争――自然，这一切在名义上都是为了帮助病人。关于这一点，本书无须重复的是，医学界领导人确实真诚地相信，限制从医人数对病人有好处。现在，我们大家已逐步熟悉了这样一种看法：即为自己谋福利就是为社会谋福利。<br />
　　随着政府在医疗事业方面发挥越来越大的作用，并且承担越来越多的医疗费用，美国医学协会的势力日趋衰落。另一垄断集团即政府官僚集团正在取代它。我们认为，这种结果部分地是由该协会本身采取的行动造成的。<br />
　　医疗事业方面的这种发展趋势是很重要的，对于我们未来将得到什么样的医疗以及为此将付出多少费用可能具有深远的影响。然而，本章讨论的是工人。不是医疗事业，因此我们只讨论与工会有关的医疗事业方面的一些经济问题，从而说明运用于所有工会活动的原理。我们将把医疗事业目前遇到的其他一些重要而使人迷惑不解的各种问题暂且放在一边。<br />
