<br />
&nbsp; &nbsp; <strong> 我现在干的事是翻译“StoryofO”，已经译到第三遍了。有些地方拿不准，就托人找老外问。有一次问到一个法国lady头上，她向我赌咒说，从来也没听说过这本书。没听说过就没听说过罢，赌咒干嘛？虽然如此，我还是字斟句琢地译着。我干这件事，是因为我相信作者有极大的才气；还因为这本书不可能出版。假如一本书有可能出版，那么奸党也会去译，并且会争到打破头；因为有稿费。但是假如一本书既没有稿费，也不可能出版，我们不译谁译。小孙看了我的译稿，说道：王二，你要是去干翻译，准是一把好笔。但是你干嘛要翻这种书？连我这妇科大夫看了都要脸红，人家能给你出吗？我说，我根本就不想出。她说，不想出译它干嘛。我没接她的茬，因为这不是我们的逻辑。再说下去就是灾难。但我也不能说，你在给我带来灾难。这样说她就会给我带来更大的灾难。<br />
<br />
&nbsp; &nbsp; 好多年前，我也说过这样的言论。那是在李先生的小屋里，抽着李先生的狗屁烟，喝着李先生的狗尿茶（那是用过期发的茶砖泡的），我在给李先生修他的狗屎收音机，一边修一边数落他。他听了不好意思，就埋头去看西夏文了。就在这时候我说，李先生，你看这玩艺干嘛？能当饭吃吗？他听了没理我。再问时就说，不能当饭吃。我又问：那你搞它干嘛？有人请你搞它吗？他再没吭声，就和没听见一样。对无聊的问题是否充耳不闻，这是我们和另一种人的分水岭。我听了小孙的话一声不吭，去拉了二十下拉力器，然后坐下来继续翻书。自从她搬进来以后，我的胸部越来越象两块门板了。小孙看着我拉拉力器，伸出一只手指抹抹鼻子，然后问：我说了什么错话了吗？我答道：没有。她听了要哭了：王二，你有什么话说哇。这么闷着干嘛。我就说：一本书，你看看它写得好不好，译得好不好就得了。害臊干什么。听了这话，她开始为自己的卑鄙言论惭愧了，就说：刚才那句话算我没讲好不好？拜托了。</strong><br />
