													<h2>齐泽克论《阿凡达》：土著的回归</h2>
						
						
																											<div id="postmessage_507883" class="t_msgfont">占士金马伦的《阿凡达》，诉说一个伤残的前海军的故事：他被派往一个遥远的、住着蓝色皮肤原住民的星球，他的任务是渗透该族群，以及游说他们让其主顾开发该星球上的天然资源。经过一连串复杂的生物控制技术，主人公终于操控得到一个年轻的原住民躯壳，即其「avatar」。 <br />
<br />
这班原住民极具灵性，并与大自然和睦共处（他们能把一条由自己身体长出来的尾巴装东西，插进马儿或大树里，与们沟通）。可以想象，男主人公当然会与某位漂亮的原住民公主相爱，然后拯救该星球，协助他们把人类侵略者扫出家门。电影的结局，主人公甚至将其灵魂由他残缺的躯壳，转移到原住民的躯壳，破釜沈舟地成为原住民的一份子。 <br />
<br />
电影的3-D效果，加上其结合真实演员及数码动画处理等，应令《阿凡达》与《Who Framed Roger Rabbit》 (1988)及《The Matrix》 (1999)等相提并论。上述电影，主人公都被卡于日常的现实及一个想象的领域――即《rogerr rabbit》的卡通世界，及《the matrix》的数码现实――还有《阿凡达》的星球里被数码处理过的日常现实。不能不察的是，《阿凡达》的叙事虽然表面上是在同一个「真实」的现实里发生，但我们需处理的――在根本的符号经济的层次上――究竟是两层现实：一层是见怪不怪的帝国主义殖民主义世界，另一层是住满原住民，在大自然过着近亲相奸的生活的幻想世界。（千万别将这层现实与真实地被剥削的人民的悲惨现实混淆）由此观之，电影的结局就是主人公彻底地由现实移民到幻想世界，如在《the matrix》里，neo决定重新沉浸回到母体里。 <br />
<br />
然而，这不表示我们应该更「真诚」地接受现实世界，从而否定《阿凡达》。事关如果我们从现实中减除幻觉，现实将失去基本的一致性，结果会是瓦解。我们并不是要从「要么接受现实，要么选择幻觉」里二选一：如果希望改变或逃出社会现实，我们首先是要改变幻觉，因幻觉把我们妥妥贴贴地装嵌在现实里。正因为《阿凡达》的主人公并没有达成这改变，所以他的主体位置，不过是拉康就萨德所言的「被其幻觉所骗」（le dupe de son fantasme）。 <br />
<br />
因此，不难想象，若《阿凡达》有续集的话，大概会是几年（或…几个月）的幸福过后，主人公开始感到某种奇异的不满，甚至开始想念那个腐败的人类世界。不满的产生，不仅因为再完美的世界迟早也会令我们失望。完美的幻觉，正正就是因为其完美，才令我们失望：完美意味着我们――带着这幻觉的主体――在其中根本没有位置。 <br />
<br />
[……] <br />
<br />
金马伦的前作《titanic》，真是一套邮轮撞向冰山的灾难片吗？要留意的是灾难的一刻：撞船发生的一刻，正是在两人确认其感情关系并回到甲板之后。更要命的是，在甲板上，当女主人公告诉其爱郎，当船到纽约后，她便会选择随他远走高飞，情愿与她的真命天子过咸鱼白菜的生活，也不要虚伪、腐朽的上流社会生活。 <br />
<br />
正正在这一刻，船便撞向冰山了，彷佛要防止其实竟无疑问将会发生的真正灾难。真正的灾难是甚么：当然是到达纽约后两小口的生活。我们当可大胆假设，日常生活同住之难，会将他们之间的爱摧毁。换言之，撞船的发生，不过是为了拯救他们之间的爱，维持某种幻觉（即如果撞船不发生，他们便会永远快快乐乐在一起）不致破灭。在最后一刻，男主人公难道不是提供了更进一步的暗示吗？当他冻得快要呜呼时，女主人公竟然是抱着浮木安然无恙。女主人公知道她快要失去爱郎时，一边说「我不会放弃你的！」说时迟那时快，她一手便把男主人公推开了。 <br />
<br />
为何如此？因为男主人公功成，便身退了。最表面的爱故事之下，《titanic》其实还有另一个故事要说：一个被宠坏的富家女的身份危机。她感到迷茫，不知如何是好；迪卡比奥因而不只是她的爱郎，他的真正身份是一个「消失的中界」（vanishing mediator），其功能是复原她对其身份的自觉，与及人生目标。他冻死于北大西洋前的说话，并不是与爱人临别的话语，而是一个传道人的讯息，忠告她要对自己诚实。 <br />
<br />
我们不应轻易被金马伦膏浅的「荷理活马克思主义」（即他粗陋地标榜草根阶层，以及卡通化处理富人的残暴和贪念）骗倒。他对穷人的同情下其实还有一层反动的迷思，这迷思最早于 Rudyard Kipling的Captains Courageous已完整地展开。故事指一个面对危机的年轻富人，经过一段短时间的实牙实齿贫困生活后，终于回复其生命力。换言之，潜藏在他们对穷人的慈悲里的，其实不过是吸血鬼式的剥削。 <br />
<br />
今时今日，荷理活看来已逐渐抛弃这种生产情侣的方程式。dan brown的angel and demon无疑就是首部改编流行小说的电影，把小说里原有的男女主人公的性爱场面在电影版本里删掉。荷理活原有的传统恰恰相反：小说原本没有的性爱场面，电影版本都会加插进去。 <br />
<br />
[……] <br />
<br />
《阿凡达》白人娶了原住民公主而成为大王的故事，其忠诚地追随传统的生产情侣方程式，其对爱的幻觉的深信不疑，令之成为一个意识型态上保守、守旧的电影。而其高超的计算机特技将这根本的保守性深深的埋藏。然而，不难发现的是，埋藏在这政治正确的主题（老实的白人支持环保的原住民，抵御「军事－工业」复合的帝国主义侵略者）下的残暴种族主义母题：一个下身残废而被地球抛弃的人，有能力转个头便便羸得美丽的原住民公主欢心，并协助一班土著保卫其星球。电影告诉我们，原住民只有两个选择，要么被人类拯救，要么被人类消灭。换言之，他们若不选择成为帝国主义现实的受害者，便得乖乖饰演白人幻想中早已分配好的角色。 <br />
<br />
正当《阿凡达》横扫全球票房时（不足三星期已大收十亿美元），一件与《阿凡达》剧本何其相似的事情却在发生。印度的住着东加里亚空达（dongria kondh）人的奥利沙邦（state of orissa）北边的山区，被卖了给矿业公司，并计划大肆开采当地丰富的铝氧石（该矿床估计价值四万亿美元）。拒绝坐以待毙，一场毛派（纳萨尔派分子 Naxalite）的武装叛乱因而爆发。 <br />
<br />
Arundhati Roy于《印度现况》杂志里，如此描述这帮毛派的游击队： <br />
<br />
… 几乎全部由穷得绝望的的部落人民组成，他们活于长期饥饿的状态――那是比得上非洲撒哈啦沙漠南部地区程度的饥荒。他们在印度所谓独立六十年后，仍然未能获得任何教育、医疗和法律补偿。几十年来他们不断被无情地剥削，小商人持续地欺骗他们，警察及林业部门的人员像{正牌地强暴他们的妇女。一直以来，因为一批毛派的同志与他们一同生活、工作及并肩作战，他们才能走上一条夺回他们尊严的道路上。部落人民今天拿起他们的武器，也是因为政府一直以来除了给他们带来暴力与忽略之外，甚么都没贡献过，今天甚至还要夺走他们唯一的财产――土地。……他们相信若今天再不为土地而战，便无异于被歼灭。这班衣衫褴褛、营养不良的部队，当中好一部份士兵连火车、巴士和小镇都没见过，现在正为生存而战。 <br />
<br />
印度总理把这叛乱形容为「单一最大型的国内安全威胁」；商业媒体把他们描述为极端地反对发展，媒体也不再报导「伊斯兰恐怖主义」了，取而代之的是铺天盖地的「赤色恐怖主义」。无怪乎印度政府以大规模的军事行动，对府印度中部森林里的「毛派大本营」。在这场战争中，两方的确都诉诸大型武力，而毛派的「人民公义」也不是省油的灯。然而，对于我等自由派的品味，无论这里的暴力是多么惹人讨厌，但我们却无任何资格谴责之。为甚么？他们的状况，正正就是黑格尔所说的乌合之众：印度纳萨尔派的反叛分子是童叟无欺的饥民，他们连生命最低限度的尊严都被剥夺。 <br />
