																											<div id="postmessage_507886" class="t_msgfont">James Cameron's Avatar tells the story of a disabled ex-marine, sent from earth to infiltrate a race of blue-skinned aboriginal people on a distant planet and persuade them to let his employer mine their homeland for natural resources. Through a complex biological manipulation, the hero's mind gains control of his &quot;avatar&quot;, in the body of a young aborigine. <br />
<br />
These aborigines are deeply spiritual and live in harmony with nature (they can plug a cable that sticks out of their body into horses and trees to communicate with them). Predictably, the marine falls in love with a beautiful aboriginal princess and joins the aborigines in battle, helping them to throw out the human invaders and saving their planet. At the film's end, the hero transposes his soul from his damaged human body to his aboriginal avatar, thus becoming one of them. <br />
<br />
Given the 3-D hyperreality of the film, with its combination of real actors and animated digital corrections, Avatar should be compared to films such as Who Framed Roger Rabbit (1988) or The Matrix (1999). In each, the hero is caught between our ordinary reality and an imagined universe - of cartoons in Roger Rabbit, of digital reality in The Matrix, or of the digitally enhanced everyday reality of the planet in Avatar. What one should thus bear in mind is that, although Avatar's narrative is supposed to take place in one and the same &quot;real&quot; reality, we are dealing - at the level of the underlying symbolic economy - with two realities: the ordinary world of imperialist colonialism on the one hand, and a fantasy world, populated by aborigines who live in an incestuous link with nature, on the other. (The latter should not be confused with the miserable reality of actual exploited peoples.) The end of the film should be read as the hero fully migrating from reality into the fantasy world - as if, in The Matrix, Neo were to decide to immerse himself again fully in the matrix. <br />
<br />
This does not mean, however, that we should reject Avatar on behalf of a more &quot;authentic&quot; acceptance of the real world. If we subtract fantasy from reality, then reality itself loses its consistency and disintegrates. To choose between &quot;either accepting reality or choosing fantasy&quot; is wrong: if we really want to change or escape our social reality, the first thing to do is change our fantasies that make us fit this reality. Because the hero of Avatar doesn't do this, his subjective position is what Jacques Lacan, with regard to de Sade, called le dupe de son fantasme. <br />
<br />
This is why it is interesting to imagine a sequel to Avatar in which, after a couple of years (or, rather, months) of bliss, the hero starts to feel a weird discontent and to miss the corrupted human universe. The source of this discontent is not only that every reality, no matter how perfect it is, sooner or later disappoints us. Such a perfect fantasy disappoints us precisely because of its perfection: what this perfection signals is that it holds no place for us, the subjects who imagine it. <br />
<br />
The utopia imagined in Avatar follows the Hollywood formula for producing a couple - the long tradition of a resigned white hero who has to go among the savages to find a proper sexual partner (just recall Dances With Wolves). In a typical Hollywood product, everything, from the fate of the Knights of the Round Table to asteroids hitting the earth, is transposed into an Oedipal narrative. The ridiculous climax of this procedure of staging great historical events as the background to the formation of a couple is Warren Beatty's Reds (1981), in which Hollywood found a way to rehabilitate the October Revolution, arguably the most traumatic historical event of the 20th century. In Reds, the couple of John Reed and Louise Bryant are in deep emotional crisis; their love is reignited when Louise watches John deliver an impassioned revolutionary speech. <br />
<br />
What follows is the couple's lovemaking, intersected with archetypal scenes from the revolution, some of which reverberate in an all too obvious way with the sex; say, when John penetrates Louise, the camera cuts to a street where a dark crowd of demonstrators envelops and stops a penetrating &quot;phallic&quot; tram - all this against the background of the singing of &quot;The Internationale&quot;. When, at the orgasmic climax, Lenin himself appears, addressing a packed hall of delegates, he is more a wise teacher overseeing the couple's love-initiation than a cold revolutionary leader. Even the October Revolution is OK, according to Hollywood, if it serves the reconstitution of a couple. <br />
<br />
In a similar way, is Cameron's previous blockbuster, Titanic, really about the catastrophe of the ship hitting the iceberg? One should be <br />
attentive to the precise moment of the catastrophe: it takes place when the young lovers (Leonardo DiCaprio and Kate Winslet), immediately after consummating their relationship, return to the ship's deck. Even more crucial is that, on deck, Winslet tells her lover that when the ship reaches New York the next morning, she will leave with him, preferring a life of poverty with her true love to a false, corrupted life among the rich. <br />
<br />
At this moment the ship hits the iceberg, in order to prevent what would undoubtedly have been the true catastrophe, namely the couple's life in New York. One can safely guess that soon the misery of everyday life would have destroyed their love. The catastrophe thus occurs in order to save their love, to sustain the illusion that, if it had not happened, they would have lived &quot;happily ever after&quot;. A further clue is provided by DiCaprio's final moments. He is freezing in the cold water, dying, while Winslet is safely floating on a large piece of wood. Aware that she is losing him, she cries &quot;I'll never let you go!&quot; - and as she says this, she pushes him away with her hands. <br />
<br />
Why? Because he has done his job. Beneath the story of a love affair, Titanic tells another story, that of a spoiled high-society girl with an identity crisis: she is confused, doesn't know what to do with herself, and DiCaprio, much more than just her love partner, is a kind of &quot;vanishing mediator&quot; whose function is to restore her sense of identity and purpose in life. His last words before he disappears into the freezing North Atlantic are not the words of a departing lover, but the message of a preacher, telling her to be honest and faithful to herself. <br />
<br />
Cameron's superficial Hollywood Marxism (his crude privileging of the lower classes and caricatural depiction of the cruel egotism of the rich) should not deceive us. Beneath this sympathy for the poor lies a reactionary myth, first fully deployed by Rudyard Kipling's Captains Courageous. It concerns a young rich person in crisis who gets his (or her) vitality estored through brief intimate contact with the full-blooded life of the poor. What lurks behind the compassion for the poor is their vampiric exploitation. <br />
<br />
But today, Hollywood increasingly seems to have abandoned this formula. The film of Dan Brown's Angels and Demons must surely be the first case of a Hollywood adaptation of a popular novel in which there is sex between the hero and the heroine in the book, but not in its film version - in clear contrast to the old tradition of adding a sex scene to a film based on a novel in which there is none. There is nothing liberating about this absence of sex; we are rather dealing with yet more proof of the phenomenon described by Alain Badiou in his &Eacute;loge de l'amour - today, in our pragmatic-narcissistic era, the very notion of falling in love, of a passionate attachment to a sexual partner, is considered obsolete and dangerous. <br />
<br />
Avatar's fidelity to the old formula of creating a couple, its full trust in fantasy, and its story of a white man marrying the aboriginal princess and becoming king, make it ideologically a rather conservative, old-fashioned film. Its technical brilliance serves to cover up this basic conservatism. It is easy to discover, beneath the politically correct themes (an honest white guy siding with ecologically sound aborigines against the &quot;military-industrial complex&quot; of the imperialist invaders), an array of brutal racist motifs: a paraplegic outcast from earth is good enough to get the hand of abeautiful local princess, and to help the natives win the decisive battle. The film teaches us that the only choice the aborigines have is to be saved by the human beings or to be destroyed by them. In other words, they can choose either to be the victim of imperialist reality, or to play their allotted role in the white man's fantasy. <br />
<br />
At the same time as Avatar is making money all around the world (it generated $1bn after less than three weeks of release), something that strangely resembles its plot is taking place. The southern hills of the Indian state of Orissa, inhabited by the Dongria Kondh people, were sold to mining companies that plan to exploit their immense reserves of bauxite (the deposits are considered to be worth at least $4trn). In reaction to this project, a Maoist (Naxalite) armed rebellion exploded. <br />
<br />
Arundhati Roy, in Outlook India magazine, writes that the Maoist guerrilla army <br />
<br />
is made up almost entirely of desperately poor tribal people living in conditions of such chronic hunger that it verges on famine of the kind we only associate with sub-Saharan Africa. They are people who, even after 60 years of India's so-called independence, have not had access to education, health care or legal redress. They are people who have been mercilessly exploited for decades, consistently cheated by small businessmen and moneylenders, the women raped as a matter of right by police and forest department personnel. Their journey back to a semblance of dignity is due in large part to the Maoist cadres who have lived and worked and fought by their sides for decades. If the tribals have taken up arms, they have done so because a government which has given them nothing but violence and neglect now wants to snatch away the last thing they have - their land . . . They believe that if they do not fight for their land, they will be annihilated . . . their ragged, malnutritioned army, the bulk of whose soldiers have never seen a train or a bus or even a small town, are fighting only for survival. <br />
<br />
The Indian prime minister characterised this rebellion as the &quot;single largest internal security threat&quot;; the big media, which present it as extremist resistance to progress, are full of stories about &quot;red terrorism&quot;, replacing stories about &quot;Islamist terrorism&quot;. No wonder the Indian state is responding with a big military operation against &quot;Maoist strongholds&quot; in the jungles of central India. And it is true that both sides are resorting to great violence in this brutal war, that the &quot;people's justice&quot; of the Maoists is harsh. However, no matter how unpalatable this violence is to our liberal taste, we have no right to condemn it. Why? Because their situation is precisely that of Hegel's rabble: the Naxalite rebels in India are starving tribal people, to whom the minimum of a dignified life is denied. <br />
<br />
So where is Cameron's film here? Nowhere: in Orissa, there are no noble princesses waiting for white heroes to seduce them and help their people, just the Maoists organising the starving farmers. The film enables us to practise a typical ideological division: sympathising with the idealised aborigines while rejecting their actual struggle. The same people who enjoy the film and admire its aboriginal rebels would in all probability turn away in horror from the Naxalites, dismissing them as murderous terrorists. The true avatar is thus Avatar itself - the film substituting for reality. <br />
