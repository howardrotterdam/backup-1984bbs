对偶然的珍惜 <br />
<br />
西闪/文 　　<br />
<br />
　　“不存在真理。”在一本书的开篇，理查德・罗蒂很干脆地宣布。作为新实用主义的开创者，当代英语世界最重要的思想家和“今日世界上最令人感兴趣的哲学家”，理查德・罗蒂正是以“不存在真理”的哲学态度赢得了广泛的关注。正因如此，很难想象这样一个哲学家会出版一本谈论弘扬爱国主义，讴歌民族自豪感的小册子。在罗蒂的概念中，真理、正义不是一些落伍陈腐的词汇吗？而它们不是爱国主义和民族自豪感的源泉吗？难道，这个善变的罗蒂又换了面孔？ <br />
　　可是这样一本书已经摆在了书架上，书名是《筑就我们的国家――20世纪美国左派思想》。 <br />
　　说罗蒂善变，并非没有根据。如今已经75岁的罗蒂当年是一位神童般的人物。就像他在《筑就我们的国家》中提到的那样，还是婴儿的时候，他就被一些著名的纽约知识分子在膝盖上举上举下，7岁的时候就在万圣节晚会上给实用主义哲学家约翰・杜威递小三明治。15岁，他已考入芝加哥大学，接着又在耶鲁大学取得博士学位。最初几年，罗蒂是新古典主义哲学家列奥・施特劳斯的信徒，但是很快他承认施特劳斯“不是一个重要的哲学家”，转而投向严格的分析哲学训练。但基本上这一时期的罗蒂研究的还是亚里士多德、笛卡尔、康德一路下来的哲学。这种哲学被称作“第一哲学”，是一种探寻事物必然性的哲学，或者所谓真理体系。后来罗蒂渐渐对此产生了怀疑，直接反映就是他的巨著《哲学和自然之镜》。在这本书中，他以分析哲学方法，对第一哲学展开了全面而有效的攻击。之后，罗蒂继而提出了新实用主义的新哲学。他的基本观点是，追求真理与追求人类较大幸福之间并无不同，因此哲学的目的不是解决那些似乎亘古不变的问题，而是对人类具有治疗性的，将那些曾经困扰前人的问题予以消解。 <br />
　　与研究必然性为目的的第一哲学不同，罗蒂的新学说的基础是“偶然”。以偶然性为基础的哲学思想并非始于罗蒂，坚信世界有一套始终处于支配地位的永恒真理的人与反对这种观念的人之间的斗争由来已久。可是尽管以赛亚・伯林后来成功地从思想史中发掘出反对者的些许思想火种，但不得不承认，随着自然科学的飞速进步，那些反对者的观念大多数被时光掩埋，并不再具备多大的力量。而罗蒂的新实用主义哲学之所以对于大多数哲学家来说既诡谲又危险，是因为罗蒂既对传统哲学了如指掌，又经过了比较先进的分析哲学方法训练，面对来自各方的诘难，他能做到应付自如。 <br />
　　罗蒂说，按照一般常识人们知道，空间和时间中的大部分东西都是人类心灵状态之外的原因所造成的结果，因此世界不是人类创造的，它自有其存在的理由。但是，人类的语言是人类所创造的，真理也是一种人类语言，是无法独立于人类心灵而存在的。虽然“世界存在那里”，但是如果不借助人类的描述，世界也就根本不存在真或假的问题。因此，“真理是被制造出来的，而不是被发现到的。” <br />
　　在罗蒂的哲学中，偶然性的地位被提到了前所未有的高度，因为在他看来，人类的语言跟兰花以及类人猿的变异一样，不过是许许多多纯粹偶然的结果，人们使用这一个词指代某个事物而不用另一个词，纯属偶然，其后果往往是不可思议和前所未有的。 <br />
　　既然追求真理与追求人类较大幸福之间并无区别，既然要致力于消解问题、治疗人心，那么就注定了罗蒂的哲学就不是一种高塔中的冥思，而是一种更好地服务于人类社会的意图。事实上，罗蒂的新实用主义为处境尴尬的哲学寻找到了一条新路，那就是放弃哲学以往自比王者或者“超级科学”的傲慢态度，宣称哲学不是什么真理体系，而是开启心智的知识、审美和道德实践。他这种以退为进的思路，重新肯定了哲学在这个“哲学终结”的时代里的价值。 <br />
　　那么，假如永恒真理已从我们的话语体系中清除，人类应该团结在什么样的共识之下呢？或者说，替代真理的，人类要追求的所谓“较大的幸福”是什么？以前的真理哲学尽管面临死亡，但毕竟在人类进步中起过重大的作用。与之相对的，以追求幸福为依归的希望哲学如何在人类进步中起推动作用，这是罗蒂必须予以回答的。我想，这也是《筑就我们的国家》一书的根本成因。罗蒂的回答必须是新实用主义在现实生活中的延伸，也应该是新现实主义理论的有力支撑。 <br />
　　在一本重要的论文集《偶然、反讽与团结》中，罗蒂从偶然性的基点出发，进一步阐释了自我的偶然性和自由主义社会的偶然性。他借用伯林的话说，文明人与野蛮人的差异，在于前者了解到个人信念只具有相对的有效性，但却能够坚定不移地捍卫这些信念。罗蒂认为，这些信念中也包括自由。所谓自由就是“对偶然的承认”。可见，罗蒂的新实用主义本质上还是一种自由主义。 <br />
　　在《筑就我们的国家》中，罗蒂一再强调弗洛伊德的主张：“我们应该把自己看作是大自然所做诸多实验之一，而不是大自然的设计之极致实现。”而这种实验，既呼应穆勒所说的“在生活中实验”，也呼应杰弗逊和杜威形容美国的民主制度时所使用的“实验”一词。 <br />
　　在书中，他极力推崇惠特曼和杜威的观点，把美国看作是“在有限的、人的、历史的努力中领会终极意义的一次机会”。在他看来，自由主义社会的众多偶然中，美国是最有可能在地面上建立起乌托邦的。在这个乌托邦中，一个高度公平文明的社会梦想“取代了关于上帝意志的知识，取代道德律令，取代历史法则，取代科学事实”，总之，以希望取代真理。因此，需要注意的是，《筑就我们的国家》中的国家是特指美国，书中的民族自豪感是特指美利坚民族的自豪感。 <br />
　　偶然、自由、实验，在罗蒂看来都是未完成的、动态的过程。 <br />
　　可是，本应承担起这个希望的美国左派却让罗蒂痛心疾首。他指出越战之后美国左派发生了分裂与蜕变，政治左派基本消失了。左派残余退缩到了学院，大搞“身份政治”、“他者研究”，蜕变成了文化左派，从而与社会实践脱离了接触，无法为民主制度、社会福利、教育改革等具体问题提出方案。他建议文化左派不应该沉溺于对资本主义制度的大而无当的批判，而是和一切认同美国理想的人联合起来，参与制定具体的政治纲领，参与制定一个《人民宪章》，参与一系列具体的改革方案，重新成为政治左派。因为他认为，右派从来不主张改革，从理论上讲，为争取社会正义而斗争的左派才是有希望的。为此，他借谈到这里，《筑就我们的国家》一书的写作目的就呼之欲出了。理查德・罗蒂之所以呼吁左派复兴与团结，是因为一个偶然性的世界更需要人类的想象力和创造性，而左派是应该担当这个责任的。作为一个自由主义者，归根结底是对偶然的珍惜，对人类自由的珍惜。</div>

							
							
							
							
