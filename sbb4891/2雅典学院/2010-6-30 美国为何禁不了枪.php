																											<div id="postmessage_712897" class="t_msgfont">美最高法院裁定：枪枝拥有权适用全国<br />
<br />
（中央社华盛顿28日法新电）美国最高法院今天裁定芝加哥禁枪令违宪，这个决定影响深远，让各州及地方政府更难以管制枪枝所有权。<br />
<br />
这对支持枪枝使用运动士来说是场大胜利。但对那些试图维持枪枝管制的民众而言，是相当大的打击。最高法院大法官阿利托（Samuel Alito）表示，宪法明定人民有权携带武器以自卫。<br />
<br />
最高法院2008年通过一项里程碑式判决，认为个人受美国宪法第2修正案保障，有权拥有包括手枪在内的枪枝。最高法院大法官今天是以5比4裁定这项判决适用于全国各城市及各州。<br />
<br />
美国“全国步枪协会” （National Rifle Association）赞扬这项裁定，并表示：“这是美国历史上重要的一刻。”<br />
<br />
华府的“暴力政策中心”（Violence PolicyCenter）则说：“这个决定会夺走人命。”（译者：中央社萧倩芸）<br />
<br />
<br />
--------<br />
美最高法院裁决禁枪令违宪<br />
<br />
【新报29日综合外电报道】美国最高法院周一（28日）裁决，芝加哥禁枪令违宪，这个决定影响深远，让各州及地方政府更难以管制枪枝拥有权，亦会引起更多欣讼。<br />
<br />
美国最高法院周一就一宗牵涉芝加哥禁枪令的案件作出违宪裁决时，最高法院大法官以5比4裁定这项判决适用于全国各城市及各州，美国每一州、每一市的人民都享有拥有枪支的宪法权利。<br />
<br />
这宗案件涉及4名芝加哥居民、两个争取拥枪权的组织和美国全国步枪协会，美国最高法院的这项裁决对他们来说是一项胜利。<br />
<br />
约9000万人有枪<br />
<br />
但是，对芝加哥州政府却是一大打击，这等于令芝加哥实施的禁枪令失效。<br />
<br />
芝加哥州政府以保障公共安全为理由，过去28年来实施严格的禁枪令。<br />
<br />
美国人的拥枪权是国内最具争议的社会、政治和法律课题。<br />
<br />
目前，美国约9,000万人有枪，他们拥有的枪支估计有2亿，但这也引发了不少罪案。<br />
<br />
根据华府的统计，每天平均有80人死于枪下，其中34人是被谋杀。</div>

							
							
							
							
