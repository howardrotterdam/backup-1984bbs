													<h2>美国为何禁不了枪</h2>
						
						
																											<div id="postmessage_712891" class="t_msgfont">关于“美国枪支管制”，上学的时候也听老师讲过，在宪法课的国家与公民关系这一部分。<br />
现在找到一篇说理还算清楚的文章，供各位了解吧。<br />
-----<br />
另：美国最具争议的社会、政治和法律课题，除了枪支问题，还有堕胎问题。<br />
<br />
<br />
----------<br />
<br />
网络文章：从美国禁不了枪谈起<br />
<br />
2008年01月31日 星期四 12:04 P.M.<br />
<br />
在美国，枪支泛滥已成为一个严重的社会问题。要是在我国，这个问题很好解决，禁止公民拥有枪支不就结了。实际上，我们也是这么做的，按照《中华人民共和国枪支管理法》的规定，持有和使用枪支只是少数人的权利，大多数人是不能拥有枪支的，如果私藏枪支，还要受到刑事处分。但是，这种做法在美国根本行不通。美国国内，有很多人呼吁政府加强对枪支的管制，但没有人提出禁止公民拥有枪支。即便如此，管枪、限枪的法律也很难在国会通过，这是为什么呢？问题的答案其实就在美国宪法之中。<br />
<br />
美国独立的时候并没有宪法，她的宪法于1787年，由出席制宪会议的各州在会上一致同意后制定。当时，制宪的主要目的是解决联邦与州的权力分配，并没有对公民权利做出规定。到了1791年，国会一口气通过了九条宪法修正案，对公民权利做了较为详细的规定，因此这九条修改案也被称为“权利法案”。美国宪法第二修正案规定：“纪律严明的民兵是保障自由州的安全所必需的，因此人民持有和携带武器的权利不得侵犯。”这一修正案实际有两层含义，一是明确了民兵的地位。在独立战争中，持有武器的民兵在打击英军上发挥重大作用，因此将民兵的地位提升至维护国家安全的高度。这一条的另一层含义就是人民持有和携带武器的权利。<br />
<br />
在我国法律中，规定公民权利的法律条文一般这样表述：“公民有……的权利”，而美国宪法对公民权利的表述则是：“……的权利不得侵犯”。这两种表述的内涵可大不一样，直接反映了立法者法治思想的不同。<br />
<br />
“公民有……的权利”体现的是权利法定的思想，社会主义法学理论认为，法是统治阶级意志的体现，权利是法律赋予的，没有法律就没有权利。而“……的权利不得侵犯”体现了“天赋人权”的思想，权利不是由法律或是什么人赐予的，而是人与生俱来就拥有的，比如《独立宣言》就开宗明义地提出：自由、平等和追求幸福是人与生俱来的权利。“持有和携带武器”在美国开国元勋的思想里，既是天赋人权，也是反抗暴政的需要。当年，北美十三州的人民正是因为无法忍受英王的暴政，不得不揭竿而起。美国人对暴政有着天然的警惕和厌恶，纵观美国宪法，无一条不是为了防止出现独裁暴政而设，即使有可能降低政府效率也在所不惜。和掌握军队、警察等国家机器相比，个人的力量是弱小的，要反抗暴政、保护自己的权利，所依靠的只有手中的武器。因此“持有和携带武器”的权利对美国人而言，非同小可，如果严格管枪还说的过去的话，那么完全禁枪则完全与美国的价值观相抵触，这也就是为什么没有人会提出完全禁枪的原因。<br />
<br />
美国禁不了枪的原因，还与修改宪法有关。因为“持有和携带武器”的权利是宪法修正案所规定的，所以要禁枪就要修宪。为保证宪法的稳定，美国宪法对修宪程序做了严格的规定。首先，宪法修正案的提出有两种方式：一是由国会两院各三分之二议员提出；二是全美三分之二的州议会提出请求，并召开制宪会议提出。可见，无论哪种修正案提出方式，其成功的难度可想而知。其次，修正案要想生效，还要有四分之三州议会或四分之三州制宪会议的批准。因此，美国宪法修正案不但提出难，生效更难。从美国宪法制定至今220年时间里，只生效了二十六条修正案，而其中九条还是在1791年生效的，这九条修正案的任何一条至今也未做过任何修改。由此可见，要想将“持有和携带武器”的权利从宪法中拿去或修改，在立法上几乎是不可能完成的任务。<br />
<br />
美国宪法之所以规定了如此严格的修改程序，这和它的产生背景有关。人们常说，美国宪法是立国宪法。这话不无道理，在美国独立后至宪法制定前的一段时期内，州的独立性很大，联邦软弱无力，整个国家仍处在内忧外患中。美国的开国先贤意识到，如果不制定宪法，确定联邦与州的权力，建立合理的政治体制，独立战争英烈的鲜血可能就要白流。因此，各州派出代表成立了制宪会议，最终制定了宪法。可以说，美国宪法是美国立国的总章程，是州与联邦之间、各阶级之间相互平衡妥协的产物，是美国政治稳定的保证。如果这种平衡和妥协轻易就能打破，势必要付出惨重的代价。因此，宪法的制定者们规定了严格的宪法修改程序，就是要保持宪法的稳定，从而保护整个政治生活的稳定。<br />
<br />
美国禁枪困难的另一个原因就是广大持枪者和枪支生产商结成了强大的利益集团，并有着代表他们利益的组织。如果那位总统或议员发动禁枪限枪运动，那么他将失去大量选票。从政治前途考虑，美国的政治家对“持有和携带武器”的权利是断不敢轻言废立的。<br />
