<br />
没想到我第一次谈“六.四”竟是由刘晓波引起的 ...... <br />
所以谈起， 是因为这本看似真诚地书， 毫不真诚地几次指名道姓地提到我。透过看似“忏<br />
悔”的烟雾，我们还是不难看出这本书中大量地、不太巧妙地、自我形象塑造，这尚可理<br />
解，这是刘晓波的风格，为了成功把“最真实”当作手段也没什么，反正总有人相信，但这<br />
一次 --- （我并未特意做过什么了解）看起来好像不大成功，至少对我谈起这本书的人，<br />
多给予不屑的评价， 当然更多的是嘲笑， 说是给人一种过了气儿的女演员、不知当今何世<br />
的感觉云云， 由此可见。 <br />
直到看完了全书， 我不由地惶惑了,没想到可邻的刘晓波已经到了这部田地了，甚至不惜三<br />
次直接了当地提到我，根据需要， 时而直呼其名，时而冠以“著名青年作家”，三次讲到<br />
我的情况，居然没有一次是事实，不是事实的“六.四”文章很多，出于各种目地写，但捏<br />
造出事实却只是为了打扮自己――大概不多见，因为这需要一些勇气，我不由地想到此书封<br />
面上的那句“中国， 你除了谎言一无所有！”的话竟然由刘晓波说出， 真有点儿讽刺的味<br />
道了， 笑过之后并问自己：你真的理解该位刘晓波的同志吗？并有点儿懂了送我此书的朋<br />
友看我时的意味深长眼光了。 <br />
写到这儿，似乎又不太想写了，这有悖我不谈“六.四”的原则。 但又一想，刘先生已经出<br />
了书，白纸黑字，事关千秋万载，虽说我不像刘晓波先生那么自爱，不仅自己老作“深刻内<br />
心反省”， 还得拉着几个作垫背；但爹娘造我一场，活到三十好几，堂堂一米九高的血肉<br />
之躯，随便让人作贱， 也太说不过去，至少我应该把刘晓波提到的几件事实澄清。 <br />
本书第一次提到我说是“五月十五号 ...... 徐星等几十人来到场 ...... 他们每人挎着肩<br />
带， 写上自己的名字和代表作的名字。 这种自视有特殊身份和知名度的出场方势， 白杰<br />
明有一针见血的分析。” <br />
我所了解的有姓名肩带的游行只有一次，（“代表作”的名字到是没见到，参加游行的朋友<br />
很多，有些人现在国外，大家可以一起回忆，是否如刘所说？ 也许刘有更准确的记忆？ ）<br />
那时根本不知刘是否在国内，见到带着肩带的队伍过来了， 我第一个反应是厌恶， 后来有<br />
人为我解释了原因。那是在四月二十七号，为了抗议“四. 二六”社论，我在复兴门加入的<br />
队伍游行到人民日报社门前，尽管我理解了为什么要这样做，但我仍没这样做， 记忆中队<br />
伍不到宣武门时，带肩带的几个人不知为什么，先后摘去了肩带，以至在人民日报社门前，<br />
互相找不到。 <br />
如果刘晓波只是道听途说也就罢了，但五月十五号我在他所说的队伍里、自从他出国以来、<br />
第一次见到他， 他也应该清楚地看见我有没有所谓“肩带”，因为我们对面坐在广场的花<br />
砖地上谈了大约五分钟，就外国女人的话题开了玩笑，刘晓波给我的印像就像一个迟到的、<br />
以为这次可以大捞一票、急着要进赌场而没有赌资的赌徒，鉴于刘的性格，我建议他先多看<br />
看，因为刚从国外回来，别先急着下“赌注”...... <br />
要“忏悔”自己尚说的过去，但在“忏悔”的时候还不惜捏造事实，仅仅为了絮叨自己的与<br />
众不同， 那么这样的“忏悔”有几分可信，也就不言而喻了。 <br />
本书第二次提到我， 是在“绝食团”的帐蓬里，” ...... 他倦缩在角落里， 走近后才能<br />
看清他沮丧的表情， 他是徐星 ...... 他说：晓波，我想来帮助他们，但是这些混蛋学生<br />
不理我，我想在广播中讲几条建议他们也不让，我说我是《无主题变奏》的作者也不管用 <br />
'” 然后一名叫程真的女学生“ ..... 趴在我耳边说：`刘老师别理他， 这人挺无赖。<br />
'”，请注意刘小波所说的程真的叙述，因为这为刘小波为什么一直被拒之门外留下了伏笔 <br />
<br />
既然被刘逼上梁山，我不得不说说当时的实际情况。 <br />
我之所以在帐蓬里，是受一个学生朋友之邀，因为他一直就在帐蓬里，他说我们写东西的<br />
人，应该看看，这是历史，我赞同这个看法。看了几天，有时也过夜， 和朋友一起帮助作<br />
了一些事务性的工作，后来被一个我很喜欢的学生领袖邀请，参加了一个“绝密”的会议，<br />
（在帐蓬外， 帐蓬里虽说如刘所说的戒备森严，但有很多人，有时甚至谁也不认识谁，学<br />
生耽心不“保密”）事前他让我讲讲看法和意见，几个人蹲在地上谈了大约几分钟，会开完<br />
了，内容完全可以对全体学生公开，无非就是下一部怎么办，我什么也没讲，我和那位同<br />
学，一起回帐蓬，他问我为什么不发表意见，我谈了我的看法，我说这是一场学生运动，是<br />
你们自己的事情，我当然也参加运动， 但有我的方式，在你们的会上谈意见不太好。 <br />
直至今天我也不知道在当时的情况下我究竟能有何“建议”要讲，而且还要如刘晓波可笑的<br />
描述，扯上我的小说。 <br />
后来在广场上一辆公共汽车旁，我向柴铃简短表示过同样的意见， 我说谁也没权力说三道<br />
四，你们什么名人也不要相信， 柴铃作了一个不屑的表情。 因为我刚刚在广场外边，“知<br />
识界联落站”目睹了一场名人们演出的活剧， 有人传来戒严的消息，“知识份子”们吓的<br />
连话都说不清楚了，因为有人在观礼台上喊了一声：“军队来了！”一些“知识份子”立既<br />
哆嗦了，（不是亲眼看见， 我真不敢相信一个人会被吓成这样）拿了东西 --- 甚至顾不上<br />
拿全， 撒腿就跑。 正是这样的、我亲眼所见的“知识份子”现在海外不断地发文章大谈<br />
“六. 四”， 俨然以英雄自居。 经厉了这些， 一点儿也不会为了毛泽东之所以轻易玩弄<br />
所谓“知识份子”于股掌之间而诧异。 <br />
再回来看看刘的书，看看吧，我“沮丧的倦缩在角落里”，想讲几条建议都不行，而他却是<br />
被“邀请来讲演的”（没写被谁邀请）还有我“说我是《无主题变奏的》作者都不管用”<br />
而他却“立刻高声说明我是谁”被指点从一个“很少有人知道的后门”进来了，当然，如果<br />
我不是如成真所说的我“挺无赖的”，那又怎么解释他在前面写到的他多次试图进入帐蓬<br />
（当时的核心层）的重重困难？显然他不“无赖”，所以他进不来。为了找回一点儿自信，<br />
“失落”了一次的刘晓波不惜用他的赫赫声名来和我这样一个既不知名也不重要的人作了一<br />
个尽管有点可怜和拙劣但还算成功的对比； 如果不加上后一段不打自招：“我接过话筒，<br />
刚才受阻时的愤怒， 屈辱和报怨一扫而光。” 可笑的是刘在外面和学生纠察队交涉的很长<br />
一段时间里，里面在商量究竟可不可以让他进来，正是我烦了，也正是我比准了让他进来，<br />
因为当时我被赋予了这个权力，我打了保票说他“可靠。” <br />
大概这也是他后来不甘失落， 作鸣人之举的绝食的心理基础。 <br />
在本书中第三次提到我，不能说一点儿也不真实，那就是我确曾要求过和刘晓波一起绝食，<br />
谈不上“三番五次”和通过他的“好朋友说情”，刘晓波的好朋友也许很多，但我只认识一<br />
个，也是我的好朋友，正是通过他，刘晓波才得以闯到我家来，我们才得以认识。但那天这<br />
位朋友并不在场。正像此时为我冠以“著名青年作家”一样，大概除我以外此时没有什么别<br />
的“著名”的人可以让他对比出他比“著名”还高大。但这次绝食背后的事情他居然一字未<br />
提，可见用别人的话来自我标榜为“有的朋友几乎毫无保留地肯定了这本书， 是到目前为<br />
止关于「八九抗议运动」的众多文字中最有价值的一本书。 它的真诚、它的严厉的自我刨<br />
析和对这场运动夹叙加议的描述，使人们看到了「八九抗议运动」的本来面目。”的书并不<br />
真诚， 如果刘所说的是真话，那么看来刘还真有几个善良的朋友。 <br />
既然叫到“真”， 就得说说我为什么要参加刘的绝食，（遭到他拒绝的原因， 他在书里说<br />
过了，我不再重复，原来他不愿别人和他一样当“英雄”，幸亏为他拒绝了，直到看到本书<br />
我才意识到险遭一场愚弄，又为绝食的其他三人婉惜，由其是我绝对不能想像周舵是为了去<br />
当英雄， 虽然我们只有一面之交， 但他给我极正派的印象，这也是朋友们的共同评价。）<br />
从五月上旬到下旬“几万知识份子大绝食”的动议一直持续，也曾有人问到我，我表示我不<br />
是知识份子，所以我不参加。五月下旬的一天下午，刘突然打电话到我家，当时我在广场上<br />
的时间很多，身心很疲惫，正在家睡觉，我妻子叫醒我接电话，刘在电话里说：他想去绝<br />
食， 让我和他一起去，并说没有任何人知道，和当时所有的“绝密”一样，他也挺“绝<br />
密”的， 说只告诉了我一个人，刘再三叮嘱我不要告诉任何人。我不同意，我问他，形势<br />
都这样了， 还有什么必要去绝食？谈的不太喻快，挂断了电话，我妻子还说了一句，“晓<br />
波他妈的发疯了” <br />
六月三号，一个朋友，告我刘明天下午在纪念碑绝食，（但我不知道还有其他三人，不知为<br />
什么那位朋友没告我）当时心里很不安，只觉得做了一件对不起朋友的事，我回家拿了几件<br />
厚衣服，不顾我妻子的坚决反对，我决定去陪他， 准时到场， 发现其他三人都很安静，<br />
而刘的绝食完全成了闹剧，并且不理解为什么“有限期绝食”，在家里免一顿不吃大概也可<br />
以叫作“有限期绝食”， 当时刘在大放卫星， 做作的悲壮表演， 这时我已经不想再加入<br />
了， 但其它三人的默默地真诚的确感染了我， 我在纪念碑前的帐蓬里和侯德建聊了一会<br />
儿，我以为我来了就是参加了，因为刘曾“绝密”地邀请过我，没想到这时却必须经刘的批<br />
准了，我简单问了一下刘，被他拒绝了，我离开了纪念碑回到了学生们中间……。 <br />
这就是刘所说的我要参加他的绝食的经过， 没想到事过三年多以后会被刘拿来当粉往脸上<br />
擦，幸亏我当时有个妻子，经历了事情的全过程，否则一切都让刘先生一个人“真实”了。<br />
<br />
书中叙述有关我的三件小事全不真实， 第二件完全可以说是胡说八道，这些小事对于整个<br />
的事件不很重要，但事关“六.四”不能不严肃，联想到出国后看到的写“六.四”的文章，<br />
多以全知全能的口气做“全景式的、完整地”描述，引起读者的反感，事实上当时群众被军<br />
队一部份一部份切割开的情况决定了没有一个人能作到“全景式地、完整地”观察， 我常<br />
想如果每个人都以对历史负责地态度，看到什么就写什么，由其是涉及到具体的人和事的时<br />
候，那将真实可信的多，而不是以刘晓波先生的这种“真实”，我不知在此书中为了刘的<br />
“坦诚、真实”，其它在书中被反复交代真名实姓的人有何感想。 <br />
<br />
现在老一套又开始了，先是尥撅子的“黑马”，然后是李泽厚，这一次是钱钟书，刘早已从<br />
自己多年来编织的梦中醒来，我们不能说刘不知当今为何世，大概只能说过了气儿的女演员<br />
还贴切一点儿。以刘的聪明我相信他深知当今的一切鸡零狗碎，我们朋友一场，我想借此机<br />
会奉告刘一句话，在“超人”的注射剂发明以前，甘做普通人苟活吧，尽管有好心的朋友乐<br />
善好施，让你搭车，也是看你赶路辛苦，并不说明你就在车上，更不说明这车就是你的。因<br />
为别人付出了常人无法比拟的代价，而你确在一味地找便宜。 <br />
<br />
对于钱钟书，我无话可说，我不喜欢任何理论，也不懂理论，我只看过小说“围城”，是我<br />
很喜欢的小说的一部，我要说的还是该刘小波，先弄弄清楚什么是“大隐”“小隐”再引用<br />
可能好一点儿，在这儿我愿意无偿地帮你一下，毕竟是老熟人嘛！“小隐隐于市，大隐隐于<br />
世”。 <br />
博士都做了，不至于记不住吧？ <br />
<br />
（其中六.四部分写于1992年）&nbsp;&nbsp;<br />
