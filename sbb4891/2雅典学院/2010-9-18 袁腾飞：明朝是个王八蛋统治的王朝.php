													<h2>袁腾飞：明朝是个王八蛋统治的王朝</h2>
						
						
																											<div id="postmessage_836058" class="t_msgfont">0 次转到微评 长短句： 于 2010-7-14 9:00:08 发布在 凯迪社区 &gt; 猫眼看人<br />
上海期间，很多媒体质疑我在《历史是个什么玩意儿》一书中对明朝的讽刺挖苦和抨击过于严重，博客中也有很多回帖质疑这一点。<br />
　　　　<br />
　　　　 我能理解这种质疑，因为明朝毕竟是汉族人统治的最后一个王朝。所有善意的质疑我都愿意接受，我只是在书中发表个人言论，我从我的出发点，得出我的结论。但那些上升到人身攻击的批评和辱骂，包括那种辱骂我为“满遗”的伪愤青言论，低级的民族主义言论，我只能抱以一笑，谁没有愤青过？但人要进步。<br />
　　　　<br />
　　　　 有人举出当年明月的《明朝那些事儿》中对明朝的一些正面评价来诘问我。老实说，我没有读过《明朝那些事儿》，因此也无从评价。但我的基本立场是：即使我不同意你的观点，我也誓死捍卫你说话的权利。每个人的出发点和角度不同，得出的结论有所不同，这很正常，唯好事者才会刻意放大分歧。<br />
　　　　<br />
　　　　以下是我在《历史是个什么玩意儿》一书第六章《最后一个汉王朝》中关于明朝的一些批评言论，可能有些过激，但确实发自内心：<br />
　　　　<br />
　　　　<br />
　　　　 “这个明朝是中国历史上最黑暗的王朝，276年里有121年皇帝不上朝，上朝也不干好事。你看那朱元璋就不干好事，一个典型的暴君。他即帝位，就是中国历史上出身最寒酸的皇帝。刘邦在前朝好歹还是街道居委会治保主任，还是奥运会志愿者，领一帮老太太还能干这个干那个呢。你说朱元璋他是个什么东西？乞丐，要饭的，皇觉寺出家为僧，让他化缘。中国和尚是不化缘的，你什么时候见过中国和尚化缘？化缘的全是骗子。中国的僧人只要化缘就全是骗子，中国的和尚是自食其力的。你像东南亚小乘佛教，那僧人都是化缘的。这化缘在中国推广不开。就因为咱们中国人认为，这算什么呀？这不是要饭嘛？那多丢人，九儒十丐比儒士还低级。东南亚那小乘佛教国家，它的僧人是过午不食，一天就吃两顿饭。中国僧人不行，因为我得干活，我吃两顿饭我下午腿软。所以你大街上碰见僧人化缘，你别理他，那都是骗子。<br />
　　　　<br />
　　　　<br />
　　　　 朱元璋后来参加红巾军，慢慢的混壮了。所以这家伙贼王八出身嘛，是中国历史上出身最寒酸的皇帝。这种王八蛋一当政，必然是采用暴政。你看那个世界历史上，什么希特勒、墨索里尼不全是贼王八出身嘛。有哪一个出身高贵，受过良好教育，上台之后就搞独裁的？没有，是吧，没听说过。准都是这么一帮人，之前寒酸，我可有今天了，我折腾死你们。包括现在那贪官污吏都是，十四岁以前没穿过鞋，穷怕了，一有权就乱来。陈水扁不就是嘛，从前我穷得不行，所以我一到现在，我可有今天了，搂吧，搂。真正像布什啊切尼啊这些人能贪污？人四千多万年薪不挣，人挣这十八万当这副总统，他贪污，你开玩笑啊。朱元璋原来叫朱重八，俩八嘛，朱俩八，二八一十六，是不是他们家十六个孩子，前面那些全死了，然后就剩他。也可能是十六号生的，是吧。你想这种东西他要是当了官，他要当了这个皇帝，他最关心的事就是我这个政权不能丢啊。千辛万苦抢过来不容易，我这皇位可不能丢了，不能让我儿子再去化缘了，得千秋万代一统江湖这么传下去。所以他要想办法在宋元的基础上进一步加强中央集权。<br />
　　　　<br />
　　　　<br />
　　　　 到了明朝，朱元璋就要吸取这个教训，不能给予宰相太大权力。洪武十三年，他说宰相胡惟庸谋反，就用这个借口杀掉了胡惟庸及其党羽两万多人，这叫胡狱。这是朱元璋的第一次文化大革命，第一次就杀掉这个胡惟庸及其属下两万多人。开国的文臣屠戮殆尽，基本上全杀光了。太祖皇帝起兵，文靠李善长，武靠徐达。李善长当时七十多岁了，风烛残年，封国公，开国功臣之首，太子朱标的老师，要上法场开刀。马皇后就不干啊，因为那个马皇后是中国历史上的贤后嘛，她就不干。不干她就不吃饭，绝食，哭。朱元璋说别的我什么都可以听你的，这件事你别管，我要为朱家开万世太平，你别管。后来这马皇后就说，你看一般的老百姓家里啊，这家长都知道尊重老师，逢年过节还给送送礼什么的，知道师恩深重。你说咱们皇家竟然要把孩子的老师杀死？朱元璋说你甭管，李善长谋反呢。马皇后听了直乐，说他七十多岁他谋什么反，他七十七了还是七十几了，他谋反能当几年皇上？他这是不可能的事么。朱元璋不听，最后李善长一家七十多口，全部杀掉，开国的文臣都杀光了。然后过年，借口大将军蓝玉谋反，这是第二次文化大革命。这又掀起一场大狱，又杀掉了一万五千多人，这一下，开国的武将也被杀光了。所以经过这个史无前例的这两次政治运动，三万五千多人啊，开国的文武功臣屠戮殆尽，九个国公，二十多个侯全杀掉。<br />
　　　　<br />
　　　　 明朝因为是贼王八建立的王朝。像唐太宗，魏征揪他衣服，抓他衣服，唐太宗说你干什么呀？你等着，我要宰了你这乡巴佬。这个唐太宗对他进行什么？威胁。唐太宗已经都给自个儿气成这样了。但是他能杀魏征吗？当然不能，朝廷律法在，你凭什么随便杀大臣，他犯什么罪了？他抓你衣服，他犯哪条罪了？朝廷有律法在，不能随便开杀戒，你随便骂大臣也不行。你骂大臣，大臣就可以抗辩。你凭什么我骂啊？你哪毕业的？你念过学吗你？你跟朱元璋说那个，哎哟，我就没念过，我是流氓我怕谁？甭来这个。朱元璋在朝廷上设立廷杖，你这个跟皇上顶嘴，一言不合怎么着啊？拉着就打。有个大臣也讨厌，给皇上上一道折子，一万七千字，你难道不知道皇上认不了那么多字？这么厚，拉下去一百板子！正打着呢，皇上看着折子，哎哟这小子说得还有点儿道理，把他召回来，已经打烂了。你想那太监打你，可有报复你的机会了，往狠里打。当然你要有钱就另当别论，所以那大臣上朝都身上揣着银票子，一看自己要受刑，赶紧给钱。你要给钱的话，拿那空板子打你，那板子是空的，听着倍儿响，啪啪啪，没事儿，就让你屁股有点儿瘀血，然后你回家就没事儿了。重点儿的就实芯板子打，最狠的是罐铅的板子，一板下去这人就完了。受杖的时候，就看监刑人的双脚，监刑人两脚分开，你死不了。双脚一闭，立毙杖下。而皇上他传令的时候他也是有讲究的，这皇上要恨这个大臣，“着实打”，那这就打死，一般的说认真打，就没事。<br />
　　　　<br />
　　　　 每个皇帝在位的时候都有打死大臣的事发生，就在午门外边受杖。朱元璋这个老东西越到晚年越厉害，这家伙可能有更年期，心理阴暗，到晚年动不动一点儿小事就动用廷杖。所以那大臣上朝之前，全家抱头痛哭啊，壮士一去不复还。够悲壮的。那辞职吧，你敢吗？你什么意思？圣天子在朝，你说不干了，什么意思，立毙杖下。没办法，只有哭，哭完了之后，还得去上朝。今天我没死，我回来了，这就是运大福大造化大。明朝这黑暗，非常黑暗。<br />
　　　　<br />
　　　　<br />
　　　　 明朝中后期政治腐败，屡出昏君啊。<br />
　　　　<br />
　　　　 比如说明武宗，就是正德皇帝。最爱干的事是调戏妇女和玩儿打仗，最爱干这两件事。他调戏妇女，是微服上街调戏妇女。后宫三千佳丽不够，还出去调戏妇女，被人逮着送县衙去了。你说君臣相见，不知道怎么脱的身。然后就是那个是在宫里开店做买卖，神武门后边开店做买卖。手里掂着那块肉，三斤三两，你上秤不带差的。皇上整天干这个，掂肉，三斤三两。开买卖，让太监什么来买他的东西。本来那太监那俸禄就微薄吧，来买他的东西。然后他少不得贱买贵卖。就这点儿钱他都榨走，你什么玩意儿这叫。然后喜欢的就是玩儿打仗，他给自个儿改名叫朱寿，封自个儿为镇国公威武大将军，什么大都督。后来有个大臣就跟他说，说你这个皇帝是君，都督是臣，不能混为一谈，你这太荒唐了。他说我有本事，百万军中取上将首级。然后那大臣就将他，您给我们表演一个。在教场，一千多明军将士，刀出鞘，弓上弦。围着一个被俘的绑在马上的蒙古将士，然后皇上冲进去，一刀给他脑袋砍下来。这叫百万军中取上将首级。百万军，自个儿的，上将绑着呢，也不知道是不是上将，反正绑上了。皇上就给咔嚓一刀，证明皇上玩儿过刀，没敲下他胳膊来。<br />
　　　　<br />
　　　　 皇上带几万铁甲兵出关，出长城，跟蒙古人打仗。打多少蒙古人呢一百来人。他带几万人，冲啊，杀，把人家消灭，然后回去报功，自个儿又升一级。将军、大将军、都督同知、都督、提督军务总兵官往上升，就这么一个东西。十年，在位十年驾崩。昏庸无度，死而无后。<br />
　　　　<br />
　　　　<br />
　　　　 明朝的皇帝一个赛着一个混蛋，一个赛着一个短命。尽是二三十岁，酒色过度就死了，尽这种玩意儿，没有一个活过朱元璋的，朱元璋活到七十多吧，没有一个能活过他，就明成祖大概六十岁。六十在明朝皇帝里算高寿了，你看乾隆爷活了89，将近90了。明武宗死了之后继位的世宗，这哥们是一个道士，整天在宫里炼丹，一年之中光炼丹用的这个燃料啊，就要二十多万两白银，也不知道他烧什么玩意儿，烧蜡弄不好。二十多万两白银炼丹。不上朝，从来不上朝，大臣几十年都见不着他。他在位四十多年，十五岁即位，四十五年。有时实在国家有大事，追问他，他不耐烦了，递出一张手谕，没人能看得懂。因为他那手谕上边，几句话他能给你写成几个字。废话，皇上写的是天书，你能看懂那还叫天书吗？最后皇上终于服用仙丹，然后就成仙了。水银中毒，成仙，汞中毒嘛。世宗就是这么一个玩意儿。<br />
　　　　<br />
　　　　<br />
　　　　 世宗完了之后穆宗即位，穆宗还不错。可惜短命，七年就死了。七年之后他死了就是神宗即位，也就是万历皇帝。神宗可真神啊，真的是神。他是就干一件事，数钱，整天盘炕上啪啪啪数钱。这就是朱元璋给他的子孙弄的这些遗传基因，都是土财主。你看人家李后主、宋徽宗，在中国艺术史上是闪耀着不灭的光辉。明朝这帮皇上，你看他这玩意儿，数钱。神宗皇帝好像一辈子就出过紫禁城一回，上昌平十三陵看看自个儿坟地，一看不错，行，修得挺好，继续。就回来了，数钱。在他住的那宫殿后头挖一大坑，埋上三百万两银子。他肥胖，然后整天在炕上盘着，左腿萎缩了。所以每天晚上太监搀着他一拐一拐的，到坑边那儿看看，我那银子还在吗？还在，这能睡得着觉，不然睡不着觉。最后那点银子全让太监给偷光了。<br />
　　　　<br />
　　　　 所以为什么十三陵考古先刨他的坟呢，定陵，先刨。他的坟里边好东西多。长陵怕有《永乐大典》不敢刨。别的皇上没他东西多，他一辈子攒钱，你想想。什么都得是往自个儿家拿。说这个地方发现煤了，派个镇守太监，把钱弄我们家来。那地方丝绸不错，派个镇守太监，都弄他们家。皇宫里边的银子，发黑变脆氧化，氧化，国库里边除了耗子屎什么都没有。所以那时候跟努尔哈赤，跟后金打仗啊。打仗说那个，咱没钱啊，国库里没钱，请皇上开内帑。不成。这打的是国仗，凭什么开内帑啊，凭什么让我掏钱？给国家打仗，凭什么让我掏钱？我不掏。没钱你加税，你让老百姓多交点税不就行啦，我才不掏这钱。后来大臣说，这江山都是您的。皇上说江山我没看见，银子我看见了。他几千万两藏起来，国库里什么都没有，然后就年年跟百姓加税。越加税老百姓不就越造反嘛，就这么一个东西。<br />
　　　　<br />
　　　　<br />
　　　　 他一死，他儿子光宗即位。光宗这哥们太光了，二十九天驾崩。登基之后第一道诏书选美女，选了八个美女，二十九天驾崩。光宗驾崩之后，儿子熹宗即位，杰出的木匠，别的皇帝上朝他下车间。据说皇上坐的椅子，睡的床全是自个儿做的。他的手艺高到什么程度，他自己做一屏风。告诉太监，拿到前门外边给我卖了，一万两银子，不许还价。你当然你不能说是皇上做的了，一万两银子，不许还价。一个小时，这太监就把一万两银票给拿回来了。就说他这个做工之精湛，值这价。<br />
　　　　<br />
　　　　 明朝中后期以来宦官专权。王振、刘瑾、魏忠贤这些大宦官专权。这是朝政最腐败的体现。东汉、唐、明这三朝，宦官专权。这个魏忠贤就专捡皇上做活的时候跟他说事，哪闹灾了。你没看我这忙呢，我今天晚上没地睡呢，哪闹灾关我什么事，我这床头柜还没做好呢，你看着办吧。<br />
　　　　<br />
　　　　 所以这魏忠贤独揽大权，魏忠贤一个字不认得，居然能跟孔子并列，配祀孔庙。全国各地到处给他建生祠，然后皇上一下圣旨，就是朕与厂臣如何如何。皇上是万岁，魏忠贤是九千岁，跟杨秀清一样，九千岁。所以你可想而知，他这个朝政的腐败程度。在这种情况下就爆发了农民起义。<br />
　　　　<br />
　　　　<br />
　　　　 郑和下西洋的目的不是发展海外贸易，不计经济效益，给明朝造成了巨大的负担。七次下西洋，600万两银子花出去，郑和整个是一个散财童子，走到哪给人家散钱，只要你奉正朔，看见我这船上没有，银子、绸子、瓶子随便。所以你得奉正朔，中国使臣搁那宣旨“奉天承运，皇帝诏曰”，人家酋长在地下跪着直乐，你们皇上是老大，狗屁！真主安拉是老大。但是一看船上正往下抬东西呢！老大！郑和来一趟，各国抢着上中国进贡来。中国厚往薄来，怀柔远人，你给我进贡一毛，我还你10块，谁不来啊！最后中国就得下令，你别天天来，我受不了。朝鲜、越南、琉球，一年一贡。你让朝鲜一年来一趟，他能来四趟，他来给点儿破布片什么的，皇帝就要回赠金银财宝，然后他的使团来了，就带着高丽参就开始卖，顺便赚一笔。<br />
　　　　<br />
　　　　 朝鲜、越南、琉球这三国因为跟中国是同种同文，几乎就是同一国家。所以这三国一年一贡。像泰国那些国家，8年来一次就完了。缅甸8年，阿富汗25年来一次，你别老来，羊肉味把皇上熏着了，你那个大胡子从来不洗。所以郑和下西洋，干这个事儿就是扯钱去了。成祖的时候去了六次。仁宗继位十个月驾崩。宣宗继位之后又下了一次，然后这个事儿就不能干了。到了明朝中期，炼丹的那个嘉靖皇帝的时候，国力衰退，有的大臣提议，为了耀扬国威，仿效当年三保太监故事，咱们再下一次西洋。兵部尚书刘大夏冲进兵部档案馆，拿起郑和下西洋的海图和船的图纸，付之一炬。因为这件事儿祸国殃民，绝不能再干。所以今天郑和下西洋到底路线怎么走的，不知道。船什么样不知道，据说郑和下西洋那个船，一百多丈长，十几丈宽，如果要是真的那么大，那相当于航空母舰了。你想64丈长，18丈宽，一丈是三米，你算算，顶上一艘轻型航母了。<br />
　　　　<br />
　　　　 不扯了，这个事儿不能再干了，太费钱了。而且一帮国家奉正朔，叫你老大，还得罩着他，西班牙、葡萄牙一欺负它，他就求你保护它，你说你管不管，像马六甲那些小国都求你。咱就得到点儿犀牛大象长脖鹿，珍珠，充实了一下皇家首饰盒和皇家动物园，我们就600万两银子扯出去。你看人家哥伦布、达伽马，什么都没有，哥几个凑钱，弄一艘小破船，带回来一个拉丁美洲，充分体现了出海的价值，你郑和跟人家没法比。”　<br />
　　　　　　<br />
　　　　<br />
　　　　 以上大约就是我抨击明朝抨击得最激烈的部分了，也是明朝的拥护者们最不能接受的。但有心的读者会发现，其实我批评的不是整个明朝的社会、人民、文化，我批评的是统治者，是那些不务正业暴虐无能灭绝人性的皇帝，我批评的是作为一个王朝存在的明朝朝廷。面对明朝的这些王八蛋统治者，我确实无法做到面带微笑客观冷静，一定有因痛恨而偏激之处，但我所骂的，只是一些最简单的每个人都知道的历史常识，常识如此，就看你用什么角度去看待这样的常识了。我说明朝不如清朝的康雍乾时代，也是从这样一个基本的人性和历史的常识出发的，请不要给我扣上“民族”的帽子，这个帽子没有常识重要。</div>

							
							
							
							
