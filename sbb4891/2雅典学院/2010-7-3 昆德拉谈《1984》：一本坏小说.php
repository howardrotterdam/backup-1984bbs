<br />
&nbsp; &nbsp; 如果说小说的形式模糊了奥威尔的思想，反之，这个思想是否给了小说一些东西呢？它是否照亮了社会学与政治学都无法进入的神秘之地？没有：是否照亮了社会学与政治学都无法进入的神秘之地？没有：境况与人物在其中像一张告示一样平淡。那么它是否至少作为推广好的思想而有一定理由呢？也不是。因为被做成小说的思想不再作为思想而运行，而恰恰是作为小说，在《一九八四》中，它们是作为差的小说，带有一部劣质小说所能运用的恶劣影响。<br />
<br />
 　　奥威尔的小说的恶劣影响在于把一个现实无情地缩减为它的纯政治的方面，在于这一方面被缩减到它的典型的消极之中。我拒绝以它有益于反对专制之恶斗争的宣传作为理由而原谅这样的缩减，因为这个恶，恰恰在于把生活缩减为政治，把政治缩减为宣传。<br />
