													<h2>北京日报社社长：旗帜鲜明地反对历史虚无主义</h2>
						
						
																											<div id="postmessage_633811" class="t_msgfont">2010年05月22日 21:50 新华网<br />
<br />
在一部分人当中也存在着实用主义、历史虚无主义等倾向，……打着“还原历史”的幌子，大作翻案文章，为已被历史淘汰的旧势力评功摆好。<br />
第一阶段为革命阶段，时间是从1911年到1949年，主要是通过革命来完成民族独立、人民解放和国家富强、人民富裕的历史任务。第二阶段为探索阶段，时间是从1949年到1978年，主要是探索社会主义的建设道路。第三阶段是改革发展阶段，时间是从1978年以来至今，主题是改革发展。<br />
<br />
　　旗帜鲜明地反对历史虚无主义――辛亥革命百年回眸<br />
<br />
　　梅宁华<br />
<br />
　　100年前，中国发生了划时代的辛亥革命。怎么看待中国这100年的历史?这是一个非常严肃的问题。历史是现实的一面镜子，现实是历史的延续。只有正确地认识历史，才能真正做到“面向未来，继往开来”；如果不能正确地认识历史，只能作出错误的判断，误导现实的发展。<br />
<br />
　　一、坚持什么样的历史观<br />
<br />
　　当前，社会上绝大多数人对中国百年历史的认识是比较客观和实事求是的，但是，不可否认，在一部分人当中也存在着实用主义、历史虚无主义等倾向，这主要表现在以下几个方面：<br />
<br />
　　一是否定和歪曲中国革命的历史。有人鼓吹所谓“告别革命”论，否定中国人民争取民族独立和人民解放而进行的反帝反封建斗争，把“五四”以来中国选择社会主义发展方向视为离开所谓的“以英美为师”的“近代文明的主流”而误入了歧路；歪曲中国共产党领导的革命和建设的历史，把它说成是一系列错误的延续，否定或掩盖它的本质和主流；更有甚者，随意丑化革命领袖和先贤烈士。<br />
<br />
　　二是刻意导演和假设历史。为否定中国的历史成就，编造和夸大所谓“人祸”，杜撰出毫无根据的“非正常死亡人数”，这实际上是在刻意导演历史。有人提出“重写历史”，用“现代化史观”取代“革命史观”，人为地把革命和现代化对立起来，经过这样的历史“解构”，近代史上的改革和革命便成了制造社会动荡、破坏经济发展、阻碍社会进步的消极力量。这种违背历史事实的主观臆想，反映了这些人对待历史的轻率态度。<br />
<br />
　　三是打着“还原历史”的幌子，大作翻案文章，为已被历史淘汰的旧势力评功摆好。有人以纠正改革开放前一些历史结论的偏向为由，为已经被中国历史发展所证明了的一些历史结论“翻案”，蓄意扭曲和颠倒历史是非；有人甚至以人性化为由，编排历史情节，对历史进行随心所欲的涂抹、剪裁和虚构，为历史上已有定论的叛徒、反动统治人物评功摆好；有人竟宣称动荡不已、民不聊生的民国时期是中国历史上的“黄金发展期”。这些显然是对历史的颠倒和虚构。<br />
<br />
　　四是“戏说”、“恶搞”历史。通过各种方式对历史人物和历史事件进行肆意戏说，对中国百年历史中的仁人志士、革命先贤进行调侃、讥笑和贬损，用主观臆想去改写中国历史；甚至在学术界，有人更是以恶搞为噱头，以颠覆正史为能事，对历史或取其一点，或无中生有，或捕风捉影……历史事实和历史真相就在这种“戏说”、“恶搞”中变得模糊起来，变得轻佻起来，成了任人打扮的“小姑娘”。<br />
<br />
　　五是追求所谓的“价值中立”和“纯客观”。有的所谓历史作品在表现人民革命战争时，只是一般性地表现战争场面的残酷，把战争和牺牲都描写成所谓“中性”的、“纯客观”的表象事实，而不去深入挖掘事实的性质，不去关注战争的人心向背和正义与否，不讲为正义而牺牲的价值意义，把中国人民为社会进步而战、为自我解放而战的正义性和崇高性虚无化。<br />
<br />
　　以上倾向和现象虽然不是主流，但却造成了严重后果，不但颠倒了历史是非，搅乱了人们的历史观，而且还消解了主流意识形态。历史观是人们关于历史的根本观点，是世界观、人生观的重要组成部分，也是社会主义核心价值体系的基础。引导人们树立科学的历史观，从而对纷繁复杂的历史现象能够作出正确的评价和判断，对人们确立正确的世界观、人生观和价值观意义极大。“灭人之国，必先去其史”，正确地认识和对待历史，是一个关乎国家兴亡的大是大非问题。是维护历史本来面目，还是歪曲历史真相；是从历史主流中吸取经验教训，还是在历史支流中制造负面影响；是坚持唯物史观，还是退回唯心史观――这些根本的原则性问题是不能被颠倒、被搅乱的，否则就会从根本上搞乱社会主流思想和主流价值，动摇一个民族、一个国家立足和发展的思想根基。<br />
<br />
　　历史虚无主义作为一种极端倾向，其要害就在于颠覆历史观，否定已有的历史结论，歪曲已经发生的历史事实，掩盖历史真相。历史结论应该是根据历史事实作出的，不能随意否定。而戏说历史、解构历史、恶搞历史，则是一种实用主义倾向，体现的是对历史的一种不严肃和不负责的态度。对中华民族来说，中国百年历史是一次伟大的洗礼，不但是血与火的洗礼，更是现代化的洗礼。通过这次洗礼，中国社会实现了跨越时代的大发展。我们应该深入地认识和总结百年历史中蕴涵的规律，正确认识和把握这个规律，将使国家和民族受益无穷。<br />
<br />
　　但是，要真正地从百年历史中找到规律性的东西，必须坚持历史唯物主义的立场、观点和方法，搞简单、片面的史料考证，不行!搞假设、解构不行!戏说、恶搞更不行!“不是人们的意识决定人们的存在，相反，是人们的社会存在决定人们的意识。”（《马克思恩格斯选集》第2卷第32页）马克思对历史唯物主义基本原理的这一经典性概括，为我们正确认识历史提供了指导，具体来说，应坚持以下原则：<br />
<br />
　　一是客观性原则。历史是什么?马克思的回答是：“‘历史’并不是把人当做达到自己目的的工具来利用的某种特殊的人格。历史不过是追求着自己目的的人的活动而已”。（《马克思恩格斯全集》第2卷第118-119页）历史作为人的活动，是一种客观存在，因此，认识和研究历史必须以事实为出发点，全面、系统地掌握有关资料，把握历史事实的总和并阐明其内在联系，透过历史现象分析历史的本质和主流，揭示历史的发展规律。<br />
<br />
　　二是包容性原则。认识和看待历史作为人的一种认知活动，必然具有多样性。因为人总是从属于某个利益阶层或群体的，不同的人具有不同的立场和观点，对历史的认识总是带有所从属利益背景的意识形态烙印。因此，对待历史要有一种包容心态，而不能用整齐划一的“单一性”思维来看待历史。搞预设立场的历史研究和片面解读，是不可能把握历史全貌的。但是，包容性原则是建立在遵循历史事实，对历史进行符合规律的探索基础之上的，而不是随心所欲地作绝对肯定或绝对否定的简单结论，更不能从一个极端走向另一个极端。<br />
<br />
　　三是是非原则。历史是有大是大非的，这种大是大非是人们对历史发展规律的揭示，是符合历史规律的价值判断。评判是非曲直的标准，不是个人的好恶，而是历史事实和社会实践。因此，对于历史大是大非的把握，不能沉湎于个人的恩怨和预设的立场，纠缠一些历史的枝节、细节，否则就无法正确认识和把握历史发展的客观规律。对于中国百年历史的认识，只有坚持马克思主义的历史观，才能科学地揭示出历史发展的规律。<br />
<br />
　　四是全面原则。这是正确认识历史的一个基本方法论原则。列宁曾指出：“在社会现象领域，没有哪种方法比胡乱抽出一些个别事实和玩弄实例更普遍、更站不住脚的了。挑选任何例子是毫不费劲的，但这没有任何意义，或者有纯粹消极的意义，因为问题完全在于，每一个别情况都有其具体的历史环境。如果从事实的整体上、从它们的联系中去掌握事实，那么，事实不仅是‘顽强的东西’，而且是绝对确凿的证据。如果不是从整体上、不是从联系中去掌握事实，如果事实是零碎的和随意挑出来的，那么，它们就只能是一种儿戏，或者连儿戏都不如。”（《列宁全集》第28卷第364页）这个论述今天仍然具有极强的现实意义。历史是一个复杂的多面体，重要的是要在把握历史活动的总和和全貌中来确定它的主要方面，分清主流和支流，这样才能揭示历史真相。如果胡乱抽出一些实例来证明自己的观点，那么任何实例都有可能被找出来，任何历史也就都可以被改写。就中国百年历史而言，革命、现代化是贯穿其中的主题，正确认识历史就在于把握历史发展的主流，揭示这些历史活动内在的逻辑和规律。<br />
<br />
&nbsp; &nbsp; 二、如何把握中国百年历史发展的本质<br />
<br />
　　历史现象往往是纷繁复杂的。在中国这样一个大国发生剧变的百年间更是如此。对中国百年历史上一系列重大事件形成实事求是的认识，必须把握历史事件的本质。对于这一百年历史，我们可根据社会发展的主题划分为三个阶段。第一阶段为革命阶段，时间是从1911年到1949年，主要是通过革命来完成民族独立、人民解放和国家富强、人民富裕的历史任务。第二阶段为探索阶段，时间是从1949年到1978年，主要是探索社会主义的建设道路。第三阶段是改革发展阶段，时间是从1978年以来至今，主题是改革发展。对这一百年历史也可作个大致的时间段划分，即三个“30年”：第一个“30年”是革命，第二个“30年”是探索，第三个“30年”是改革发展。<br />
<br />
　　在第一个“30年”的革命阶段，中国社会的剧烈变革像一曲多重变奏的交响曲。辛亥革命、五四运动、北伐战争、全民抗战、解放战争等一连串的政治变迁构成了社会转型的主旋律。只有用历史唯物主义的立场、观点和方法，才能正确地认识这些重大历史事件。<br />
<br />
　　关于辛亥革命。对以孙中山为首的资产阶级革命派领导的辛亥革命到底该怎么看?至今仍有一些问题未能彻底搞清楚。比如，辛亥革命究竟是失败了还是胜利了?首先应承认辛亥革命是中国历史上第一次完整意义上的资产阶级民族民主革命，其功绩和历史地位是不可抹杀的，它推翻了清王朝的统治，给中国带来了近世文明，使民主共和的观念深入人心。这是不容否定的历史事实。但是也要看到，辛亥革命并没有完成反帝反封建的任务，没有改变中国半封建、半殖民地的性质，也没有能够实现国家统一，国家仍处于军阀割据和军阀混战的分裂状态。这也是不容掩盖和抹杀的历史事实。所以，从这个意义上说，辛亥革命是不彻底的。其所以出现这样的结局，是由当时的历史条件所决定的，领导这次革命的中国民族资产阶级无法与帝国主义和封建旧势力彻底决裂，导致革命胜利果实为袁世凯代表的军阀势力攫取。辛亥革命的历史也说明，中国民族资产阶级不可能领导中国民主革命取得彻底胜利，在中国走资本主义道路是行不通的，这不是主观臆断，而是历史作出的客观结论。<br />
<br />
　　关于五四运动。五四运动已过去90多年了，但至今仍有一个很关键的问题没有彻底搞清楚，这就是五四运动的主题究竟是什么?有“启蒙”说、“新文化运动”说、“反帝反封建”说，等等。那么，五四运动究竟是一场什么样的运动?从历史事实上看，五四运动是爱国知识分子为了反对帝国主义列强在巴黎和会上损害中国主权、反对当时政府的卖国政策而发动的。运动后期，中国工人阶级的参与，标志着运动突破了学生、知识分子的狭小范围，发展成为有工人阶级、小资产阶级和民族资产阶级参加的全国范围的群众性爱国运动。当然，五四运动也是一次追求民主、科学的思想解放运动，标志着中国革命进入了一个新阶段，成为新旧民主主义革命的分水岭。此后，中国无产阶级及其政党登上了政治舞台，并找到了救亡图存的革命道路。因此，五四运动从根本上说是要寻找中国救亡图存之路，其主题非常明确，就是“救亡图存”，其全部内涵体现在中华人民共和国国歌中。尊重历史事实，就不应该淡化这个主题。<br />
<br />
　　关于北伐战争。1926年5月，叶挺独立团揭开了北伐的序幕。北伐刚开始之所以出现势如破竹的胜利局面，是因为孙中山制定的“联俄、联共、扶助农工”的新民主主义三大政策符合民心。但是，随着北伐的节节胜利，以蒋介石为首的国民党右翼势力，为独吞革命的胜利果实，背离了三大政策，搞清党反共，大肆屠杀共产党人和革命人士，公然走上了背叛革命的道路，致使国共合作破裂，北伐遭到挫折，造成了历史的倒退。这也是国民党必然衰亡的根本原因。现在，竟有人不顾蒋介石屠杀共产党人、叛变革命的事实，极力为其屠杀共产党人、制造反革命事端的罪行开脱。历史事实是，蒋介石叛变革命后，中国陷入新军阀混战，给中国人民造成了空前深重的灾难。<br />
<br />
　　关于全民抗战。相对于过去一段时间里很少提及国民党在正面战场抗战中作用的情况，近年来有人又走向了另一个极端，即片面夸大国民党在抗战中的功绩，仿佛抗日战争只有国民党进行了“英勇抵抗”，作出了“重大牺牲”，而极力回避和抹杀国民党在抗战期间“积极反共”、“消极抗战”的事实，这当然是裁剪和歪曲历史事实、掩盖历史真相的做法。事实上，抗日战争是中华民族的伟大胜利，不仅仅是哪个党派的功劳。对于国民党正面战场抗战，我们应该有这样几点基本认识：一是抗日战争是在国共合作的旗帜下进行的全民族抗战，国民党领导的正面战场和共产党领导的敌后战场都作出了贡献。二是应当看到蒋介石作为中国当时的最高统治者，有抗战的愿望，但是他的抗战是三心二意的。在“九・一八”事变民族危机空前严重的关头，他还坚持“攘外必先安内”的不抵抗政策，把主要军力用来“围剿”共产党和工农红军，对日本的疯狂侵略一味妥协退让，以致造成“望风而逃”、“失地千里”的溃败事实，特别是在抗战相持阶段国民党“积极反共”、“消极抗日”这一历史事实更是不容抹杀。三是国民党内部分化严重，派系林立，有亲日的，有亲美的，还有亲德的，各有算盘，纷争不断，很难作到统一意志，一致抗日。汪伪汉奸就是从国民党分化出来的。四是共产党作为抗日民族统一战线的制定者和贯彻者，抗日最坚决、最彻底。共产党及其武装力量之所以在敌后根据地发展壮大起来，绝不是国民党散布的“保存实力”，而是因为共产党坚持全民抗战方针，抗日最坚决，在敌后实行的土地改革政策和民主政权建设，符合广大人民群众的意愿要求，维护和实现了他们的利益，因而只有共产党才能够真正做到发动和组织群众起来抗击日本侵略，打一场抗日的人民战争。因此，抗日战争的胜利是中华民族全民抗战的结果，不应夸大国民党正面战场的作用。<br />
<br />
　　关于三年解放战争。抗日战争胜利后，和平是国际国内的大势所趋和人心所向。中国共产党为顺应人民的愿望，提出了“民主统一，和平建国”，积极争取和平建国的新局面。但是，国民党无视人民的和平愿望，仗着美国的支持和援助，背信弃义地撕毁停战协定，发动了大规模的反共反人民的内战，使中国社会又陷入了内战危机之中。内战开始，国民党在军事、经济方面的实力都大大超过共产党。军事方面，国民党军队达430万，而且装备精良；而中国共产党领导的人民解放军只有120万，且装备简陋。国共军事力量对比如此悬殊，为什么中国共产党领导的人民解放战争能够以弱胜强、迅速胜利?连毛泽东都未料到国民党在三年之中就被打垮。其根本原因是，中国共产党代表广大人民利益，团结了要求实现国家统一和民族独立愿望的先进知识分子、工人、农民、民族资产阶级等革命力量，实现了符合中国人民要求的一系列政策。比如，土地政策使农民真正成为农村和土地的主人，极大调动了农民的革命积极性，参军支援前线成为热潮。正如毛泽东所指出，“我们的解放战争，主要就是靠这一亿六千万人民打胜的。有了土地改革这个胜利，才有了打倒蒋介石的胜利”。（《建国以来毛泽东文稿》第1册，中央文献出版社1987年版，第397页）那么，为什么抗日战争胜利以后中国社会没有实行“联合政府”?这决不能责怪共产党。中国共产党诚心诚意地希望国内和平、避免内战，在抗战胜利前夕就提出了成立“联合政府”的主张，但是国民党却违背人民的愿望和要求，妄图在中国搞独裁统治。<br />
<br />
　　通过对几个重大事件和重大问题的分析，我们可以对革命阶段的历史有这样一个基本的认识：鸦片战争后，中国逐渐沦为半殖民地半封建国家，中华民族面临着实现民族独立、人民解放和国家富强、人民富裕这两大历史任务。由于反动统治势力不愿自动退出历史舞台，前一个任务只能通过革命的手段来完成。但是，这场革命必须是彻彻底底的革命，而且只有在代表先进生产力发展要求的中国共产党领导下才能取得最终胜利，这就是中国近代社会的发展逻辑。没有发生在中国大地上的中国共产党领导的这场伟大革命，中国社会就不可能“脱胎换骨”，走向民族复兴。任何否定中国革命的说法，都是站不住脚的。<br />
<br />
&nbsp; &nbsp; 第二个“30年”的探索阶段，是新中国历史上最为复杂和曲折的历史时期。社会主义改造、大跃进、人民公社运动、“文化大革命”是这段历史中绕不开的重大事件。近年来，对于这30年历史的认识，出现了一些违背历史唯物主义的态度和原则的观点，其中有两种观点值得注意。一种观点否定中国进行社会主义改造的必然性，认为中国不该急于向社会主义过渡，应该按照毛泽东民主革命时期提出的“新民主主义社会”的构想，让资本主义有一个充分的发展，等到条件成熟后，再进行社会主义革命，进入社会主义社会。另一种观点全盘否定这30年的探索历史，把其看成是一系列错误的延续，认为在中国搞社会主义是“历史的误会”。这些观点，无论从历史事实还是今天的现实来看，都是对历史的歪曲和主观臆断。事实上，只要坚持历史唯物主义的立场、观点和方法来看待这段历史，就能正确揭示出这段历史中蕴涵的必然性。<br />
<br />
　　首先，社会主义改造是中国历史发展的必然。1949年新中国成立后，中国进入新民主主义社会，但是这个社会并非一个独立的社会形态，必然要过渡到社会主义社会，因为新民主主义革命的前途是社会主义，新民主主义革命胜利为社会主义革命创造了条件。建国初期在党的领导下，经过3年国民经济恢复时期之后，中国的工农业生产无论在规模上、速度上，还是在主要产品质量上都取得了历史上前所未有的水平，这为其后向社会主义过渡准备了条件。于是，1953年党明确提出了过渡时期总路线，提出逐步实现国家的社会主义工业化，并逐步实现国家对农业、手工业和资本主义工商业的社会主义改造。这条路线的实质是为了确立社会主义生产关系，使之成为国家和社会的惟一经济基础，直接地是为了解决生产资料所有制问题，间接地是为了更快地发展生产力以实现国家的工业化、现代化，反映了中国社会发展的历史必然性和中国社会主义道路选择的独创性。对于社会主义改造，邓小平强调指出：“在搞社会主义方面，毛泽东主席的最大功劳是将马克思列宁主义的普遍真理同中国革命的具体实践结合起来。我们最成功的是社会主义改造。”（《邓小平文选》第2卷第313页）<br />
<br />
　　其次，探索过程不可能不犯错误。如何建设社会主义?这对刚刚成立的新中国来说是一个崭新的历史课题。从1956年到1976年，党努力从实际出发探索社会主义建设之路。其间尽管遭受许多挫折，甚至出现严重失误，但还是提出了许多关于社会主义建设的有价值的思想和判断，也取得了一些社会主义建设成就。对这30年探索采取全盘否定的态度，是很不严肃、很不科学的。如果按照历史唯物主义的观点和方法来分析，就会发现这段曲折历史的背后有着深刻的必然性。新中国成立后，我们党面临着“立什么”的重大课题，虽然经过社会主义改造确立了社会主义制度，但党对“什么是社会主义，怎样建设社会主义”这个重大问题一时还很难认识清楚，只能在实践中摸索。只要是探索，就有可能成功，也有可能失败。在社会主义制度刚建立后的20年探索中，由于指导思想上出现了“左”的错误，致使出现了“大跃进”、人民公社化运动、“文革”等重大曲折，使社会主义实践遭到严重挫折。但是，在付出代价的同时，这30年的探索也为国家的发展创造了一系列根本性条件：社会主义的政治根基――四项基本原则是这一时期形成的，人民民主专政的国体、人民代表大会制度的政体以及党领导的多党合作和政治协商制度等社会主义的基本制度是这一时期确立的，独立的国民经济体系也是这一时期建立的，所有这些为后来改革开放的强劲发展奠定了政治和物质基础。正如胡锦涛同志在党的十七大报告中所指出的：“改革开放伟大事业，是在以毛泽东同志为核心的党的第一代中央领导集体创立毛泽东思想，带领全党全国各族人民建立新中国、取得社会主义革命和建设伟大成就以及艰辛探索社会主义建设规律取得宝贵经验的基础上进行的。新民主主义革命的胜利，社会主义基本制度的建立，为当代中国一切发展进步奠定了根本政治前提和制度基础。”而且，中国共产党从这一时期所犯的错误中所总结的一系列经验教训，也是建设社会主义的一笔宝贵财富。邓小平曾指出：“没有‘文化大革命’的教训，就不可能制定十一届三中全会以来的思想、政治、组织路线和一系列政策。三中全会确定将工作重点由以阶级斗争为纲转到以发展生产力、建设四个现代化为中心，受到了全党和全国人民的拥护。为什么呢?就是因为有‘文化大革命’作比较，‘文化大革命’变成了我们的财富”。（《邓小平文选》第3卷第272页）党和人民从这几十年曲折探索中深刻地认识到，搞教条主义是不行的，闭关自守也是不行的，这为改革开放奠定了认知上的基础。<br />
<br />
　　第三个“30年”的改革发展阶段，是中国百年历史上最为光辉的篇章。在这30年里，中国经济持续快速发展，国家经济实力和综合国力大幅度提升，人民生活水平得到了前所未有的改善。中国社会主义事业在经历前30年曲折探索的艰苦磨砺之后迎来新的辉煌，中国人民在党的领导下成功开辟了中国特色社会主义道路，创造了令世界瞩目的“中国奇迹”，把社会主义现代化和实现中华民族伟大复兴的宏图伟业推到了前所未有的新阶段。改革发展30年的历史雄辩地证明：改革开放是强国之路，是决定中国命运的关键抉择，必须继续坚持不动摇、不懈怠、不折腾。对于这30年的历史，今天绝大多数人都能客观和实事求是地认识和看待。<br />
<br />
　　总之，中国百年历史是一个不可分割的进程，革命、探索、发展这三个阶段的发展演进具有无法抗拒的历史逻辑。中国今天的巨大进步，是几代为中华民族复兴奋斗的革命者、建设者的伟大实践而取得的成果，决不能把这一完整的历史脉络割裂开来。<br />
<br />
(作者：北京日报社社长，《红旗文稿》2010年第10期）<br />
