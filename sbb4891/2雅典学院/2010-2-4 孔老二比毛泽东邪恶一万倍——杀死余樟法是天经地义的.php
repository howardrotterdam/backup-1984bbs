做一个负责任的大人<br />
<br />
一<br />
网络兴起，给匿名写作和发表提供了特别的方便。大多数网民们“闯荡江湖”时<br />
习惯于披上千奇百怪的马甲，原因很多，其中之一当是某种可以理解的怯懦恐惧心理所致。很多人纵然说的是如实、合理、合法的话，却怕惹麻烦，不愿不敢留下真名，以致出现了“在现实生活中人们用真名说假话，在虚拟世界里人们用假名说真话”的国内互联网独特奇观。<br />
<br />
一般信息交流思想商榷及一般性批评，用假名披马甲无妨。但是，在指名道姓地对他人进行批判、指控，特别是涉及他人人格和名誉时，匿名就有所不妥。一般网民和知识分子也罢了，公共知识分子乃至儒者这么做，就有自我轻贱、不负责任之嫌，故有必要提醒一下：<br />
<br />
中国要在国际上做一个负责任的大国，儒者也要在人际间做一个负责任的大人。对自己观点和言论的负责（负道德与法律之责），这在儒家乃是最基本的要求，尤其是指名道姓地批判他人特别是涉及他人人格和名誉时，应该亮出真身，光明正大。这也是对自己的尊重。谨以此与儒家同仁及有志之士共勉。<br />
<br />
<br />
二<br />
用真名不见得说的就是真话、正话，更不一定就是大人，不仁不义不诚无耻之徒纵用真名，说的也会是假话、邪话。但敢于真名亮相，至少证明有一定的担当，愿意为自己的言行负责。<br />
<br />
匿名攻击、化名泼污、暗箭伤人，乃民胍岳聪铝魑娜说奶爻ぃ在互联网时代更加发扬光大起来。不实的攻击、非理的批判、无礼的污辱，便是真名也非所宜，况匿名乎？<br />
<br />
不少人物喜欢躲在马甲之后恣意攻击、恶意污辱他人，不以为耻反自以为聪明，不仅对网络环境及社会道德造成严重污染，而且也恰恰暴露了其内在的怯懦、阴暗和丑陋。（躲在马甲之后高叫为自己的攻击负法律责任，恰是最不负责任的。）儒者当引以为鉴。我起草并发表于2003、7、16的《关于用真名说真话的倡议书》曾表明态度如下，值得重申一下：<br />
<br />
“当我们在互联网上发表思想、观点和有关公共事务的言论时，只要不涉及严格意义上的国家机密和个人隐私，不论是批评还是赞美，是指斥偏谬、抨击错误、批判邪恶、反对专制还是宣传弘扬一切真善美的事物，我们将尽力做到用真名说真话，赞不谗谀，批要合理，一切以良知本性为出发点，以事实和真理为标准，绝不说没有根据、不合事实和言不由衷的话，绝不曲学阿世。我们对自己的言论负责，承担由此造成的一切后果，并接受公众和广大网民的监督。面对无所不在无孔不入的假，我们的人生原则是：说真话、用真名、做真人。由于个人视界和思想的局限，更由于有关信息人为的闭塞和有意的误导，真话是相对的，不成熟、不合理乃至错误之处都是难免的。我们欢迎批评指正、指错批谬。”<br />
<br />
所谓用真名亮真身，倒不一定每文皆署、每处皆注哉婷实姓。不少网络名家尽管是网名，但略有“江湖经验”者不难了解也不难找到其本人，这就与用真名无异。东海真身余樟法，是在世纪之初、上网两个多月就“亮”出来了。（见附文《我来剥“东海一枭”的皮》）<br />
<br />
三<br />
我在《我来剥“东海一枭”的皮》中说“至于详细地址及电话手机等，这里就不公布了，不是胆怯而是怕闲杂人等打扰我清修。”而今，对于那些江湖混混、下流文痞之类闲杂人等，不仅现实生活中，便是网络上也不会再轻易“失言”、更不再有任何交往交流的兴趣。（当然，若有人一定要“扰”上门来，东海自当水来土掩、开关奉陪。）<br />
