附枭眼看人之十六：我来剥“东海一枭”的皮（发于2001、11、29）<br />
上网二月，发贴百多，恣意论诗，放言议政，更刀向嘘太君，枪挑余大少，拳战风二侠，剑敌xxx，戟戳狼九鸣，酒斗谭壮飞，鞭击袁子才，纵横江湖，瞎玩胡闹，树“敌”一大堆，交友一大片，何乐如之。<br />
余杰曾痛斥网上匿名者并表示，他不会与匿名者讨论任何问题，老枭在《枭眼看人之----枪挑余杰》中已予以驳斥。老枭以为，网上攻守，属于观点之争，思想之战，真名网名，个人爱好，不是什么原则性问题。杨支柱君日前跟贴曰：<br />
“点名批评文章不用真实姓名的确不妥，这是自己躲在屏障后向人家发冷箭，不公平。至于使用匿名破口大骂之辈就更等而下之了。我并非一律反对匿名，但攻击个人的文章不应该匿名。如本篇攻击余杰的文章即是。”<br />
又有为民君接着跟贴：“在一个无法无天，到处都有危险的社会，匿名是一种不得已的办法。什么时候网上不删贴，言论有自由，政府背后不打冷枪，我才同意北大才子的说法。”<br />
倒象老枭是个喜欢暗里发冷箭的小人和怕事不敢露头的软骨头似的。<br />
在《枭眼看人之五》我早说过，匿名上网，是大隐隐于网，过一把蒙面大侠的瘾，“痛快淋漓放手大斗几百个回合，或单打、或混战”，多好玩哪。老枭大好男儿，堂堂正正光明正大，可不愿被人小看了，这就自个剥去“东海一枭”的皮，亮出洒家真面目来。虽然自知从此不得安宁矣。但我宁戴棘冠甚至南冠，宁为徒呈匹夫之勇的莽汉，也不愿头上被扣懦夫的小帽（只是愧对妻儿了）。至于曾自称年届八一，乃戏言，已多次跟贴言明实乃妄攀党龄。<br />
日前亦凡文学报做我专辑要作者简介，我写道：东海一枭，雄性，属龙，龙性难驯，潜龙勿用；名枭，魂游物外，声闻九天。身隐于商，志托于诗。诗则新旧双栖，性则刚柔相济。龚定庵名句：亦侠亦侠亦温文，差堪仿佛。读书数万卷，创作几千篇（首），出书近十册（多属自费）。贪美酒美色美景，爱奇石奇书奇人，暂隐西湖。电邮xxx。<br />
太虚太简了，今奉上一则简介，是拙著作者简绍，比较实：<br />
萧瑶，原名余樟法，男，1964年12月10日出生于浙江九龙山下，现居杭州。八十年代以来，创作了大量的新诗、旧体诗词、散文、杂文、随笔等，陆续结集出版了新诗集《浪子吟》（香港金陵书社出版公司1993年版）、《未必逍遥》（民族出版社1993年版）、《剑魂琴心》（广西人民出版社1996年版）、《在命运之上──中国诗人自选诗丛&#8226;萧瑶卷》（作家出版社1998年版）、散文集《呼唤英雄》、旧体诗词集逍遥山庄诗稿》及续集、三集、四集（均由银河出版社出版）。&nbsp; &nbsp;&nbsp; &nbsp; <br />
至于详细地址及电话手机等，这里就不公布了，不是胆怯怕事而是怕闲杂人等打扰我清修。至于东海一枭的文责，当然由我负。线索多多，有关部门要找我，自是探曩取物。萧某生平交友无数，结怨犯忌也无数，该来的都来吧！<br />
两个多月来，许多网友或贴或函，与我讨论问题，争辨是非，不论褒贬赞骂，我都表示感谢。你们的网名，都将保存在我记忆的硬盘里。两个多月的相处，已与东海一枭这个网名产生了感情，今后，允许我仍以此名上网。只不过真 相一露，从此多事，且失去了蒙面混斗、大吹其牛的乐趣，以后上网机会和兴趣，定然不多矣。最后以七律一首，与朋友们共勉：<br />
吹箫说剑送流年，一角湖山别有天。腹底凝香书十万，坛前屠狗客三千。春残愁看群芳谢，薪尽勉将野火传。吟罢云低风渐紧，且将孤胆护婵娟。-----《自题说剑堂》东海一枭2001、11、29</div>

							
							
							
							
