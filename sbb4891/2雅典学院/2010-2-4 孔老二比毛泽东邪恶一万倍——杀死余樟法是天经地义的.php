原则上人人皆有良知，但某些人物及群体良知受蔽深重，在相当长时间内将难以唤醒。为了不致造成时间精力与“表情”的过大浪费，暂时放弃才是最明智的选择。孟子说得好，不屑教诲也是一种教诲。<br />
<br />
另外，有人以为东海变成了木鸟和老人，是背叛了敢言敢怒的一枭，要推卸责任、要当老好人了。这是皮毛之见。东海当然是好人，永远是（但不是老好人）。木鸟对一枭是超越，是好上加好，是更加尊人和自尊，对中华文化、对国家社会对他人更加负责。木鸟对一枭的文章当然负完全的责任，当年以直报怨的处世态度也不会改变----只是更宽容大度了，不会再将各种小恩怨放在眼里。<br />
<br />
东海呆若木鸟，没有杀气了，是柔的更柔刚的更刚，心更明眼更亮“内功”更精纯，处事接物更镇定、冷静和理智了。重任自肩的东海，将会更加仁义智慧地面对一切，随时准备着迎向一切，包括头顶的乌云、眼前的风雨及背后的暗箭！2009-10-20东海老人首发《民主论坛》<br />
