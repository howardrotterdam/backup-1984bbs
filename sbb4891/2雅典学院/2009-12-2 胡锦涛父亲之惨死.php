																											<div id="postmessage_352245" class="t_msgfont">50元，两桌酒席，泰县饭店，在1978年而言，算得上是很大的排场。因为当时的50 元，相当于现在的1500元；当时的泰县饭店，是江苏泰县（现泰州市）最高档的饭店。这是当年在青海任副处级干部的胡锦涛，返回故乡安葬父亲时，特意摆下的宴席，希望该县领导高抬贵手，为其亡父平反。 <br />
然而，宴席从中午摆到下午3点，都没有一个领导露面。3点多之后，才有一名县委办公室的主任到来，说县里和供销社的领导今天一直在开会，只能派他一人前来，算是跟胡 锦涛涛打个招呼。胡和亲友坐在饭桌旁，面对两大桌酒席，只好唉声叹气。最后，胡将该饭店所有人员，包括厨房师傅、跑堂的、洗碗的，全部叫到一起，请他们帮忙，将这两桌高档酒菜吃了。眼看这些原本素不相识的人吃得不亦乐乎，胡一家人只好强作笑脸，有苦难言。<br />
<br />
胡锦涛的父亲胡静之，祖籍安徽绩溪。清朝末年，祖上迁到江苏泰县上坝，以卖茶叶为生。胡静之继承祖业，也操此业为生。胡母李文瑞是泰县白米镇胡家店村人。 &quot;公私合营&quot;时，胡的茶叶店难逃厄运，他本人成了泰县供销社的一名职工。文革中，胡得罪了当地的造反派，被扣上&quot;贪污&quot;罪名，惨遭迫害，先后被批斗关押，身体垮了下来。文革结束后，年仅50多岁的胡静之含恨离世。<br />
<br />
听到父亲去世的噩耗后，胡锦涛赶回泰县奔丧。他们父子情深，胡7岁丧母，胡静之一直没有再娶，而将3个孩子送到妻子的舅母处抚养成人。在安葬父亲前，胡锦涛找到当时泰县县长陆某及供销社领导，请求为亡父平反，开一纸证明。当时有些好心人劝胡在当时泰县最高档的饭店──泰县饭店摆两桌酒请领导们过去&quot;喝喝酒、谈谈心&quot;，但遭冷遇，没有结果，胡只好返回青海。从此，泰州成为胡锦涛的伤心之地，他一直对自己的出生地避而不谈，26年来再也没有回过故乡。<br />
<br />
胡锦涛继任国家主席后，江苏泰州当局决定，代胡修祖坟；当年胡就读过的两所学校，也保留了胡读书的教室，没有拆迁；当地领导还策划了一连串与之相关的&quot;政绩工程&quot;，以便让胡返乡&quot;祭祖&quot;时看到。然而，事与愿违，胡从没有返乡&quot;祭祖&quot;。那些所谓&quot;政绩工程&quot;，反倒在泰州民间，引起怨声载道。</div>

							
							
							
							
