																											<div id="postmessage_753346" class="t_msgfont">苏小和炮轰郎咸平：他无知 为赚大钱刻意迎合大众和权力<br />
2010年07月22日 07:21时代周报<br />
“唐骏们”的精准卡位术<br />
<br />
唐骏、郎咸平两个人这几年在中国风生水起，不仅名满江湖，而且财源广进。他们是成功的典范，还是青年人的偶像。两个人有相同之处，也有明显的差别。至少，唐骏的博士来自野鸡大学，而郎咸平的专业学问和教授身份，怀疑的人就少之又少。<br />
<br />
大概是在9年前，我去采访过唐骏，精致的西装，夸张的表情，言必谈毛泽东和魂牵梦萦的祖国，当然也肯定会谈到他发明的卡拉OK打分器。这么多年过去了，今天已经红遍大半个中国的唐骏，依然是把这些关键词挂在自己的嘴巴上。我想起当年对他的印象，他说自己是加州理工大学博士，我就心存向往，那是一个多么严谨、多么具有开放性和创造性的大学啊，可是我在几个小时的对话里，完全找不到唐骏的知识结构和价值谱系，他似乎只是为了适应今天的生活在努力构建自己的话语系统，好让自己和这个时代、这个国家，尤其是这个国家的主流话语协调起来。<br />
<br />
回头来看，唐骏当然是聪明的，他把自己准确地卡在爱国主义和领袖风范位置上，充分发挥他在海外多年闯荡积累起来的一点经验，叫那些头脑简单、渴望成功的年轻人疯狂追随。唐骏知道这样的精准卡位是安全的，更是可以游刃有余的，在这个路径上，他可以竭尽全力地表达，可以像一名伟大的演说家一样，面对广场上数不清的人头，激情四溢地演讲，事实上唐骏的确具有表演天才，他的语速不快不慢，懂得句子的抑扬顿挫，年轻人岂有不欢呼、不追捧的道理。<br />
<br />
对唐骏进行量化分析，他的技术来自两个方面。<br />
<br />
其一当然是领袖气质，这是大批中国人的集体爱好，唐骏借着传销般极具蛊惑力的手法，轻而易举把领袖这样的大词安在了自己的身上。但如今所有人都知道唐骏只是唐骏，他再怎么折腾，也不是领袖，人们也知道，让人顶礼膜拜的领袖时代已经远去，人们更需要一个看上去有点像领袖的有钱人,于是,“天价年薪”适时地出现了。这才是这个时代中国人对成功的基本定义，领袖气质，富可敌国，才是真正的偶像。<br />
<br />
第二种技术，是唐骏先生的海外精英身份，必须是名牌大学的博士，必须是世界一流大公司的高级管理人员。中国是一个很有意思的国度，那么多人高喊着爱国主义口号，攥着自己的小拳头自强不息，仇美反日抗法烦英国，但遇到每个从海外留学回来的人们，却都投以羡慕的眼光。唐骏深知这样的道理，微软这样有面子的公司身份，他当然是有了，而著名美国大学的博士学位，几乎所有知道唐骏的中国人，都相信他肯定是一个了不起的博士，谁曾想到，他原来不是，他说了假话。<br />
<br />
像一枚金币，唐骏的一面是领袖、一面是博士，缺少了其中任何一面，唐骏知道自己在中国就玩不转了，可叹方舟子不谙世道，也不近人情，非得闹出唐骏原来是西太平洋[11.55 -0.09%]大学博士毕业的好戏，唐骏能认错么，他不会的，他的知识结构没有教给他忏悔的品质，他大好的金钱梦想也不允许他认错。余下的就只有一条路，死猪不怕开水烫，在中国，除了民间的一些刁民反复纠缠，体制不会拿唐骏怎么样。<br />
<br />
再看郎咸平。<br />
<br />
新华书店里，郎咸平的书现在那可是门庭若市，而最火的一本，书名叫做《郎咸平说：新帝国主义在中国》，事实上他的另外几本书名都非常咋呼的图书，照样名列销售排行榜的前几名。前两年，郎咸平到处放言，反对企业制度改良，提醒国人要防止国有资产流失，好像他才是真正维护经济公平的伟大学者。但稍微有点眼光的人必然知道，中国经济多年以来最大的问题，还是现代企业制度不够健全，诸如产权问题、公司的治理结构问题等，一直是国有企业的大患。即使是最保守的管理高层，也深知只有让国有企业的股权结构公众化、国际化、资本市场化，中国的国有企业才有可能形成真正的效率。这事实上是对历史的一种纠正、对现实的一种重组。老百姓多年以来受制于国家主义意识形态的熏陶，不注重自己本身的个人权利，有一些理解上的误区尚在情理之中，但郎咸平这个接受过最好的现代企业管理科学教育的教授，却在这里著书立说、迎合大众、拒绝常识，他究竟想干什么？<br />
<br />
一名真正的读书人，或者说一名具有独立精神的读书人，既不能迎合权力、迎合资本，也不能迎合大众。事实上，郎咸平不仅刻意迎合大众，而且迎合权力的厚黑技巧，甚至超过了真正在内地出生、长大的绝大多数学者。最近这几本“声讨新帝国主义”的图书，算是郎咸平迎合权力的大好贡品。帝国主义这个冷战时代的关键词，如今在郎咸平的笔下，似乎又火起来了。但问题在于，今天已经是一个全球贸易的时代，尤其是中国，甚至已经是一个对出口贸易有严重路径依赖的国家。可以这么说，在冷战时代，被人为隔离的贸易框架，可能的确带有意识形态的侵略性质，但在今天，当全球化成为一个经济学事实，郎咸平再次祭出帝国主义的陈旧词汇，就是一种对经济学分工理论和全球贸易理论的无知。<br />
<br />
和唐骏一样，郎咸平要想在中国赚得大钱，爱国是必须要坚守的姿态，这是他经过思考后选择的一种话语姿态。现在看来，郎咸平似乎成了中国利益的捍卫者，成为民众利益的代言人。这样的市场卡位迅速给郎咸平带来了具体的利益，他出场演讲的身价节节攀升，而几乎所有请他演讲过的人都知道，郎教授在出场费上，一直是锱铢必较、毫不客气。据说，遇到不能谈妥价格的演讲，教授是可以临时撂摊子的。而他的一系列热气腾腾的书，基本占据了所有图书营销排行榜的前几名。郎咸平教授在今天的中国，不仅是一个言论的基地，也是一个赢利的工具，各路人马围绕着他马不停蹄，赚得盆满钵满。</div>

							
							
							
							
