																											<div id="postmessage_333319" class="t_msgfont">面对陈志武热：起来，不愿被金融的人们……<br />
刘松萝<br />
<br />
近来，陈志武携《金融的逻辑》，刮起了一场旋风。陈志武提出了不少好的主张，比如“政府有钱不如民间富有”。对于金融知识的普及，《金融的逻辑》也是有作用的。<br />
<br />
同时，陈志武也在一些地方颠倒黑白。他说：“在全球金融危机冲击下，从中国到美国、到其他发展中国家，各国政府又在将企业国有化、强化政府对资源的控制。在这种时候，我们尤其有必要重新认识自由、民主、法治的财产和金融基础。”中国存在这种状况，而美国就不是了。<br />
<br />
我们知道，美国金融危机的发生，有着复杂的原因。银行家和企业家为了追逐高额利润热衷于投机，无疑是重要的原因。按照陈志武喜爱的自由经济原则，那些惹祸的公司，应该让她们倒闭才是。但是，接下来的问题出现了。假如听任金融机构一个又一个地破产，经济运行的链条就要被切断，更大的灾难就会出现。我在《抵御金融危机：经济战争或者经济紧急状态》中说：“谈论自由经济很容易，处理问题却很难。大厦摇摇欲坠的时候，需要用外力企稳，不要让它倒了，然后再讲其他的事情。汽车就要开下悬崖的时候，首先要做的是紧急刹车。多米诺效应已经形成的时候，当务之急是切断其传递的链条。此时拘泥于自由经济的教条，就是误国误民。”<br />
<br />
国家的干预，在布什政府最后的几个月就开始了。美国，并不如陈志武所说的那样将企业国有化，而只是加以救助，待情况转好之后退出企业。对于这一点，陈志武并没有否认。如此说来，陈志武前面的话无疑就是武断了。我想，这样说话多少有些得便宜卖乖。<br />
<br />
更加值得重视的不是《金融的逻辑》本身，而是一些经济界的强势人物以及民间的经济学爱好者的反响。美国金融危机爆发以后，他们安静了一段时间，不再鼓动金融创新了。振兴经济的政策，又让他们活跃起来。如今，投机活动如火如荼，房价节节攀升。就在大家玩得心跳又欲罢不能的时候，《金融的逻辑》给那些记吃不记打的投机者们吃了定心丸，打了鸡血。当然，这不完全是陈志武的责任。<br />
<br />
陈志武有两个重大的缺陷。第一，在一个领域被认可之后，轻率地对很多事情指手画脚，去讲总的和基本的道理。第二，成为一元论者，用狭隘的合算与不合算的道理去指导他并不熟悉的事情。说起来，这也算是经济学家的通病。以我个人的印象，这里面多少有些成名之后的自我膨胀。<br />
<br />
金融的逻辑很重要，但是金融的逻辑不能压倒其他的逻辑，更不能成为指导社会活动的唯一的逻辑。针对金融的逻辑，我想说的是，控制风险，并且让金融为民众的福祉而不是富豪的贪欲服务，也是金融的逻辑。进一步说，这不仅仅是金融的逻辑，而且应该成为金融的伦理，金融的铁律。<br />
<br />
按照金融的逻辑，当房地产投机已经进入炽热阶段以后，经济学家不应该再去鼓动这种投机，因为这会对社会有害。退一步讲，即使为财阀着想，即使作为理财顾问，也不要怂恿他们去参与这种金融活动。实实在在地说，这种活动早就不是金融了。<br />
<br />
包括金融活动在内的经济活动有可能产生两种结果：社会总财富的增加，以及类似赌博的零和游戏。不幸的是，如今有人借《金融的逻辑》鼓动的金融活动，主要就是零和游戏。在零和游戏中，一部分人的所得来自另一部分人的所失。一百种《金融的逻辑》，或者加上别的什么逻辑也无法改变这一事实。<br />
<br />
在中国，明星经济学家的作用，通常是利用自己的名望，让人们继续相信已经破绽百出的道理。厉以宁张维迎臭了，还有茅于轼；茅于轼臭了，陈志武就出现了。等到陈志武臭了，张志武李志武们还有新的道理，新的名目在等着我们呢。就这样地接力下去，足可以让没有思想又想与众不同的人们从20岁一直相信到古稀之年，一如既往地替强势者数钱。鞠躬尽瘁，死而后已。<br />
<br />
没有被洗脑的人们，应该不难看到现实的残酷。不断升级的房地产投机，是把民生必需的住房作为投机的筹码，是在裹挟整个社会，让大众去承担投机的后果。<br />
<br />
在谈到人口红利的时候，网友押沙龙有一个跟帖，大意是：我看人口红利的说法也是不靠谱的。人口红利，不过是失业人口红利，贫困人口红利。这对雇主自然是好事，但对被红利的人们呢？<br />
<br />
押沙龙提到了一个说法，被红利。我想，把住房作为投资品，投资者的收益，无疑就来自未来购房者更大的付出。于是，我们就被投资了。这样的局面，是难以为继的。<br />
<br />
《金融的逻辑》值得一看，只是不要说风就是雨，不要看得走火入魔。学养差的人，容易看到新鲜一些的文章就以为是天书，就以为得到了终极的真理。最重要的是，当有人利用《金融的逻辑》继续吹大经济泡沫的时候，我们应该有所警惕。<br />
<br />
美国的金融危机，实际上就是整个社会被少数投资者和投机者金融了。因此，比金融的逻辑更重要的，是我们应该有不被金融的权利。<br />
<br />
起来，不愿被金融的人们……<br />
