　　中国经济的巨大泡沫已经让中国处于难以处理的地步，但是目前对冲基金正忙于做空欧元，无暇顾及也暂时没有好办法做空中国。等到欧洲攻势告一段落，预计他们会在香港等相关区域先做多红筹股票和相关中国经济股票，以取得泡沫和人民币升值带来的利润。<br />
　　<br />
　　当泡沫破裂之时，或者大鳄推波助澜，抛空人民币，同时沽空恒指，做空大宗商品和资源股。那就是中国经济崩溃的黑天鹅事件。而捅破泡沫的只有两个可能，一是中国政府自己捅破；二是美国经济复苏，美元强势回归的时刻。<br />
　　<br />
　　这个时刻离现在有2到5年的时间。<br />
　　<br />
　　我们的希望在何处？<br />
　　<br />
　　开放民营经济，全面肯定民营体系在各个经济领域的公平权利，从而带动就业和经济转型，鼓励农村城市化向大城市群发展，彻底开放户籍政策，从而用10年的城市化延续泡沫的缓慢涨缩.<br />
　　<br />
　　中小城市的农村城市化是没有出路的，只有大城市群才能节约公共资源，释放粮食土地压力，同时带来产业升级和服务业的发展，减税，鼓励创产业升级，但不是要消灭低级制造业，在设计、行销、品牌、科技这些方面增强低级制造业的综合能力。<br />
　　<br />
　　而我知道，是没有几个人会赞同实行这些政策的。<br />
　　<br />
　　其他国家：跛足的熊和逐步崛起<br />
　　<br />
　　俄罗斯：当能源价格下跌时，俄罗斯就不但失去了自己的矛，也失去了自己的财富制造机器。在叶利卿之后，俄罗斯重回威权体系，经济上的沙文主义在商品泡沫阶段非常强势，在经济萧条阶段，就显示整个经济体系缺少活力的危机。在十年之内，俄罗斯不会再走回强大。<br />
　　<br />
　　印度：以其强大的内生市场、It服务业、健康的金融体系，造就了缓慢却充满活力的庞大经济体。在保障公平、牺牲效率之后的今天，他们也开始学习中国的开放政策，加强外资吸引和国际贸易。印度实行了三十多年的国营经济体系，造成普遍贫穷，和官商勾结之后，这十几年来印度的自由经济政策，已经带给世界刮目相看的局面。而民主体系造就的动态平衡社会，也使这个经济体的维稳成本极低。他们正在缓慢崛起！<br />
　　<br />
　　巴西：巴西是金砖四国中最不为人所知的国度，但却是活力无限的国度。她的政体稳定、资源丰富、制造业发达、能源和生物能源丰富。同时，巴西摒弃了阿根廷等国僵化的《劳工法》，实行的是类似德国体系的劳工保护制度，既保证了劳工收入，也保证企业主不被讹诈，整个经济体充满了外向型和内生需求兼顾的发展潜力，而整个南美洲共同体的打造，更为它提供了广阔的经济舞台。<br />
　　<br />
　　结语：应邀写一些分析，试图找到经济和历史发展的轨迹，不知道未来如何变化。我们不能够准确预测未来，因为我们只是历史卑微的卷入者，唯有人性和行为可以揣摩。<br />
　　<br />
　　后面的几年是世界经济的垃圾时间，会有赚钱的机会，赚钱也已经是智慧富人的游戏，而不是富人当然就是穷人，富人也会贫穷，而穷人机会更少。<br />
　　<br />
　　未来的几年没有创造财富、带给人类社会大发展的机会。我们尚不能看到IT奇迹的重新到来，在200年前是蒸汽机，80年前是汽车业，20年前是电脑和互联网技术，现在除了泡沫和失业率，没有任何希望。低碳经济是一种假象和政治故事，没有多大意义。<br />
　　<br />
　　我依旧期待这块土地上的勤劳人们继续制造辉煌，因为他们已经制造了三十年的辉煌，不要给于恩惠，只要给予公平，给予自由的发展空间，他们就会给世界一个惊喜。我相信中国人的聪明、勤劳，如果有信仰、习俗，知道善恶和是非，知道价值代表什么，知道恐惧和感恩，那么，我们或许得到的不仅仅是一个财富之地，而且是一片人间乐土！<br />
　　<br />
　　 <br />
　　<br />
　　 ----写于3月，为《中国商人》提供专栏稿件　</div>

							
							
							
							
