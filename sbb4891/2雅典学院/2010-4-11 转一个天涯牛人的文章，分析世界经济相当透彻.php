世界经济的垃圾时段<br />
 作者：海边的老王　回复日期：2010-04-11<br />
　　平面媒体用稿，请勿转载。<br />
　　<br />
　　在以往的20年里，华尔街的表兄们一直嘲笑日本金融界的表弟们：你们把垃圾扫进地毯底下，假装看不见，就以为它不存在了？<br />
　　<br />
　　这句话让日本寒碜了许多年，而日本的经济也失落了20年。自广场协议之后，日本的经济导向采取了一系列错误政策，从流动性的极度宽松产生庞大泡沫，到猛然紧缩，导致泡沫爆裂；而后长达十数年的低利率和巨额赤字政策，使日元沦为融资货币，但却不能迫使日本经济恢复。虽然中国经济近10年来的猛烈增长，给了日本短暂辉煌，但是当年日本金融界掩耳盗铃，隐藏隐形负资产，同时财阀经济窒息中小企业创新和融资，终于导致经济在未来的10年里仍旧看不到曙光。<br />
　　<br />
　　不过总算日本人有些面子，因为现在美国、欧洲和中国三大经济体都步入同一条河流，大家难兄难弟，一起捂着眼睛，眼不见为净，开始鸵鸟政策，兼顾以邻为壑。<br />
　　<br />
　　美国：庞氏的阳谋<br />
　　<br />
　　很难说美国制造了阴谋，但实际上司马昭之心，路人皆知，称之为庞氏的阳谋。<br />
　　<br />
　　当奥巴马参加总统竞选时，老王持反对态度，并在网络专栏里规劝华人社会不要支持民主党，不想却引来骂声遍野，从太平洋两岸席卷而来。偶像的力量巨大，奥巴马在美国大陆和中国大陆赢得粉丝无数。<br />
　　<br />
　　从奥巴马的性格来说，他身段柔软，从来不会坚定自己的立场，时刻按照环境改变；从他的主张来说，他主张大政府的干涉，自命为民生的救赎者，这违背了美国建立联邦民主体系的原则，小政府、区域自治和自我奋斗；从他的支持者来说，工会和非洲裔群体成为坚强的支持者。<br />
　　<br />
　　我在当时说，奥巴马上台之后一定会开展对中国的贸易保护战，一定会放纵美元贬值，迫使人民币升值。当然美国现在有了意外收获，欧元暴跌，导致美元避险功能提升，强力反弹，他一定会大力印刷货币，玩赤字游戏，向美国国民、世界、未来借钱。<br />
　　<br />
　　没有多少人相信。<br />
　　<br />
　　美国人救助了华尔街，把数十万亿美元的金融垃圾产品扫进了地毯的角落里，全然不顾那里鼓起一个难看的包，就当世界上的人都是傻瓜，看不到房间的脊梁被压的塌陷，楼下正在掉落粉末。中国人被挟持着继续持有数千亿的两房不良债券，美国人假装用国家信用担保他们不再是垃圾，中国人假装相信自己还有不少财富，然后，中国人又自愿为娼增持美元债券。当然，实际上中国也没有多少选择，买矿产，矿产暴涨；买黄金，整个西方恨不得你赶快包圆；买欧元，希腊把老欧洲的虚弱显示给所有人看。<br />
　　<br />
　　这给了美国政府和华尔街勇气，让他们把古老的庞氏游戏玩个彻底。当我们看到美国经济这些日子的数据，就会明白一切都没有起色。不错，美国的经济增长率开始复苏，这只是因为库存在去年被消耗殆尽后增加库存产生的假象。当你看到十个点左右的失业率，就会明白一切都没有开始。<br />
　　<br />
　　这导致了奥巴马政府的支持率下降，导致民主党在肯尼迪参议员死去后遗留的位置丢失，从而导致奥巴马可笑的全民医疗改革计划有可能失败，而他也会失去自己的基础――支持者的信心。所以他必须呼啸起全面的贸易保护主义，以满足工会和其成员的迫害狂臆想：中国人导致了他们失业！<br />
　　<br />
　　他还要再挥舞汇率大棒，指责中国操控人民币汇率，人为制造贬值促进出口，抢夺美国劳工就业岗位。尽管我也认为中国的确人为制造了人民币贬值，但抢夺美国劳工就业岗位却是放屁。没有中国，还会有其他国家，当世界贫富差距存在，人的收入差异太大，就一定会产生资本的流动向低成本地带扩散。当美国工会强劲，资本流动，丢失掉一些传统职位；当阿根廷、非洲许多国家工会强劲，劳工法强势到要把企业主烫皮去血，他们失去的不仅仅是就业机会，而是整个经济的动荡。<br />
　　<br />
　　当中国人还没想清楚自己处于什么位置，2007年的人民币升值、《劳工法》新版出台、降低出口退税，基本上就自我阉割了。好在美国发生了金融危机，给了我们一块遮羞布：你看，是邻居的捣蛋鬼推倒了我家围墙。但这只能用来遮住露出的冻疮，不能自我臆淫，我们很强大，你看美国佬快完蛋了。<br />
　　<br />
　　且住，美国佬正在往你家里倒垃圾呢。<br />
　　<br />
　　美国人玩弄借贷游戏已经出神如化，一方面向全世界收取铸币税，美元作为世界货币存在一天，他就可以收取一天的红利，这让美国肆无忌惮四处借贷，美国人也依靠借贷消费；另一方面，美国人玩弄金融游戏，让美元持续贬值，从而让债务缩水，并刺激经济复苏。除此之外，其他货币的虚弱也让美元流动性时常增加。<br />
　　<br />
　　玩的就是心跳。<br />
　　<br />
　　美国人最大的问题不在于美元衰落，美元在看得到的数十年里都是世界货币。美国最大的问题是奥巴马代表的自由主义分子，就是我们所称的左派精英，试图颠覆美国反智主义、自我奋斗、自由自主的传统，以大政府的全面介入来代替自由经济的复苏。<br />
　　<br />
　　美国医疗改革的推动，似乎在向欧洲学习，但奥巴马政府根本没明白，欧洲实行全民福利的基础在于有美国军事的保护，从而节约了军费预算，可以用于全民福利。即使是这样，也导致了欧洲经济多年蹒跚，无法前行，现在美国居然掉头来学习欧洲。<br />
　　<br />
　　同时，美国大规模的介入经济，投放货币，制造了数十年来最大的通货膨胀泡沫，他们想通过投放货币向未来和全世界征税，导致美国中小企业成本提高，资产泡沫的高涨吸收中小企业的融资，然而，中小企业的复苏是劳工就业提高的基础，加之大政府的存在加剧了财政支出，同时美国军事预算不断提高，通货膨胀的泡沫因此被越吹越大。与之对应的泡沫出现在中国的房地产业，这里面隐藏的危机，导致世界经济存在第二波崩溃的可能性。<br />
　　<br />
　　人性的懒惰和贪婪，导致美国人注定要走捷径来对付经济的迷失：向全球转嫁货币债务成本，向中国转移失业造成的政治压力，向气候要钱，再制造另一个泡沫代替资产泡沫，如果不行，终极解决方式，发动一场区域战争，促进军工生产，美元回流。<br />
　　<br />
　　欧洲：皇帝的新衣<br />
　　<br />
　　索罗斯时常说一些妙语，比如他早就认为欧元不成气候，因为只有一个统一的央行，却没有一个统一的财政部，如何保证货币的信用不被各怀私心的十几个小兄弟拖垮？<br />
　　<br />
