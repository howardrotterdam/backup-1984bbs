													<h2>何清涟：“六张网”救不了中国的极权统治</h2>
						
						
																											<div id="postmessage_388956" class="t_msgfont">何清涟<br />
<br />
就在“世界人权日”之前，中共中央机关刊物《求是》杂志登载了国家公安部部长孟建柱的长篇文章，主要内容是谈如何加强政府部门处置“群体性事件”（即社会反抗）的能力，其中特别提到要加强“六张网”的建设。这种视民如寇雠并张网以待的方式，与“建立和谐社会”的提法正好形成绝妙的反讽。<br />
<br />
何谓“六张网”？孟建柱解释得非常清楚，它们是街面防控网、社区防控网、单位内部防控网、视频监控网、区域警务协作网和“虚拟社会”防控网。中国当局希望通过这“六张网”，织成一张防止一切反抗的天罗地网，“实现对动态社会的全方位、全天候、无缝隙、立体化覆盖。” 更何况，这“六张网”只是常规监控，每逢有庆典或者需要防范的一些特殊日子，比如“六四”、“两会”期间等等，当局还要再启动“奥运安保模式”，除了让警察扮装成便衣，与巡防队员、保安员等职业队伍全部上街之外，还动员“志愿者”如治保积极分子、单位“门前三包”人员等“社会力量”，用“人民战争”的方式消灭 一切可能的反对力量。<br />
<br />
当局这种草木皆兵的紧张心态正好表明其政治上的极度虚弱。这种虚弱，缘于中国政府已经堕落为自我服务型的自利型政治集团。当局者很清楚自身的执政地位既缺乏政治合法性，政府官员的严重贪腐更成为社会仇恨蔓延的根源。杨佳事件与唐福珍事件引发的民意表达有如一面镜子，让他们从中看到了自身那极为丑恶的面目。<br />
<br />
视民如寇雠并张网以待，在中国历史上也曾出现过，只是那大都是在王朝末期，统治者为挽救即将倾覆的统治地位而采取的手段。其中最著名的可能要算是明末崇祯年间兵部尚书杨嗣昌提出的“四正六隅十面网”之策。当时，明朝廷已经被长达十余年此起彼伏的农民起义拖累得精疲力尽，为了镇压最活跃的李自成等部，杨嗣昌提出“四正六隅十面网”的战术构想和 “攘外必先安内”的外交方略。杨嗣昌之策是以陕西、河南、湖广、江北为“四正”，分剿而专防；延绥、山西、山东、江南、江西、四川为“六隅”，分防而协剿。后来洪承畴将此方略稍加变更， 将“四正”定为东南西北；“六隅”定为山、川、江、湖、乡、镇。所谓“十面网”，则是用各省官军十面围逼，围而不打；由五省总督亲率十万精锐，专攻专打。<br />
<br />
杨嗣昌此策初用之时，农民军确实大大失利。崇祯高兴之余，写了一首诗赐给杨嗣昌，其中有“盐梅今暂作干城”之句。但问题是，明末农民起义这场烈火根源在于吏治败坏，民不聊生。杨嗣昌的“十面网”只能暂时起消防队的作用，如何化剑为犁，让百姓安于畴亩躬耕为业，从根本上消灭动乱之源，崇祯及满朝文武已经无力回天。因此，这“十面网”虽然一织再织，明王朝最后还是无法逃脱倾覆之命运。<br />
<br />
中国政府现在编织的这“六张网”加上“奥运安保模式”的威力，当然远非明朝那种纯粹的人力之网可比。即使与当年苏联克格勃全盛时期那种“老大哥在看着你”的监控模式相比，“六张网”也要严密与先进得多。可以说，某个人一旦被当局列为“异类”，在“六张网”下几乎插翅难逃：在家里，有邻舍与小区的治保积极分子在看着你，说不定你家中还被悄悄安装了秘密摄像头（这是社区防控网）；出门上街则有“街面防控网”、上班则有“单位内部防控网”、上网还有金盾工程为主的“虚拟社会防控网”，即使这几个地方都被侥幸逃脱，还有视频监控网与区域警务协作网。中国政府及其少数御用文人总鼓吹中国眼下进入千年难遇的太平盛世，但这“六张网”的编织，却实实在在地证明着今天的中国民怨鼎沸，社会仇恨一触即发。<br />
<br />
依靠民脂民膏以自肥的中国统治集团面对充满怨恨的民众，其惊慌程度可与当年前秦苻坚在淝水之战后败逃途中闻到风声鹤唳时的状态相比。崇祯时期那“四正六隅十面网”，是用来对付遍地燃烧的反抗烽火，即叛民；而今天这“六张网”，却是用来监控全体民众。这意味着今天的统治者已经视全体人民为潜在敌人。非统治集团成员如果还将这个政府视为自己的政府，纯粹属于自作多情。<br />
<br />
不过，有民乃有国，民意、民心才是一个国家存在的坚实基础。纵观人类历史，还没有一个政权与民众势成水火还能长久屹立；用枪杆子、笔杆子加上特务统治这种非常规方式统治一个人口最多、贫富差距居世界前列的国家，固然可求一时之“稳定”，但要想长治久安，却有如缘木求鱼。无怪有传言称，胡锦涛目前最大的心愿就是“平安下车”。但领导者个人的平安下车不等于政治集团的平安下车，更不等于这个国家能够和平发展。作为一位中国知识分子，我真诚地希望胡锦涛能模仿当年的蒋经国先生，不求做中华民主政治的创建者，只求做最后一位独裁者。如此，则中华幸甚，中国人幸甚，胡氏本人也将以正面形象而名留青史。<br />
