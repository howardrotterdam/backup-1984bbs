Tear Down This Wall 【钓鱼岛显然属于日本】<br />
推倒柏林墙 @ 2010-9-23 1:39<br />
<br />
<br />
自钓鱼岛撞船事件以来，最近网上又掀起了一股论证该岛“自古以来就是中国神曙N而不可分割的一部分” 的风潮。这钓鱼岛可能古代的时候还序&#12539;#23454;是中国的一部分，但是到43年之后还是不是可就不好说了。如果I&#65437;看过台湾热血保钓男马英九在“钓鱼台列屿问题学术研讨会”上的发言《钓鱼台问题简析》，就会发现里面有一段看似轻描淡写、实则庶&#65434;常蛋疼的文字：<br />
<br />
“二次大战后，罗斯福跟史障U林想把琉球交给中国，但是中国觉得琉球当年是日本占领的，所以希望琉球独立。结果是先由美军先托管，以后再说。美军托管琉球时发布的第一号命令，就是所R出托管区，而公布的托管区经纬度地图包含钓鱼台列屿，但是当时中华民国政府却没有反应。为何会如此？有人立委质询过后来的外交部政务次长沈剑虹先生，他说是为了区域的安全，所以没有表示意见。”<br />
<br />
不仅是马英九，在所&#65439;一篇关于“钓鱼台列屿问题学术研讨会”的报导《争钓鱼台，国际法不利台湾》中，几位学者都提到问题的关键就在于蒋介石在二战时期主动放弃了琉球群岛的主权，并且在未来的二十多年里从来没有提出过任何庶&#65434;议。大陆这边同样有多篇文章提到，在1943年开罗会议期间，蒋介石曾携王宠惠与罗斯福单独谈话，席间罗斯福询问蒋介石是否想要索回琉球主权，蒋介石却回答：“我觉得此群岛应由中美I&#65382;国占领，然后国际托管给中美共同管理为好。”I&#65382;天后罗斯福再次序&#12539;#35748;了蒋介石对琉球的看法，在得到相同的答复后认定中方已经放弃了琉球主权。<br />
<br />
大陆的网络上所流传的文章大多没有引用来源（只有一篇引用了张其昀的《开罗会议纪实》），但此说并非毫无根据。美国国务院出版的《Foreign Relations of the United State: The Conferences at Cairo and Tehran, 1943》（此书有免费PDF版：<a href="http://digicoll.library.wisc.edu/cgi-bin/FRUS/FRUS-" target="_blank">http://digicoll.library.wisc.edu/cgi-bin/FRUS/FRUS-</a> idx?id=FRUS.FRUS1943CairoTehran）第324页“Chinese Summury Record”里便有如下记载：<br />
<br />
“The President then refered to the question of the Ryukyu Islands and enquired more than once whether China would want the Ryukyus. The Generalissimo replied that China would be agreeable to joint occupation of the Ryukyus by China and the United State and, eventually, joint administration by the two countries under the trusteeship of an international organization.”<br />
<br />
蒋介石参加开罗会议的主要目的之一便是收复澎湖列岛等失地，为何对别人双手奉上的琉球群岛反而一而再再而三的推三阻四？有人说这是因为蒋介石惧怕日本人，有人说美国人开的是空头支票，老蒋自己则向美方表示，不希望美国认为中国有在战后扩张领土的野心。这些观点从逻辑上来讲都有些说不通，个人认为复旦大学历史系教授所&#12539;i平在接受《南方周末》采访时（<a href="http://www.infzm.com/content/6798" target="_blank">http://www.infzm.com/content/6798</a>）所给出的解释最为靠谱：<br />
<br />
“根据1943年5月宋和罗斯福总统的谈话记录，宋子文正式提出过，反法西斯战争结束以后，中国要收回东北及台湾、琉球，罗斯福表示赞成。但是在同年的开罗会议期间，蒋向英美方面谈到琉球主权问题时，表示中国只要求参加战后和美国一起对琉球的管理，属于国际托管，不要求收回。为什么会出现上述差别所H我认为重要因素之一，是蒋宋关系在开罗会议前的史迪威事件中破裂了，蒋震怒之下，拒不见宋，不再让宋处理外交公务，宋无法出席开罗会议。如此重要的国际会议，外交部长却不能出席，岂非咄咄怪事？后来去的那些幕僚，可能根本就不知道宋和罗斯福有过那样的谈话。”<br />
<br />
根据所&#12539;i平的理论，可以想象出这么一副场景：蒋介石对于自己在开罗会议上要争回暑]些领土，本来都是照本宣科，这时美方突然抛出一个计所R外的琉球群岛，老蒋顿时手足无措。为了防止在罗斯福面前露怯，还得硬着头皮回答，于是乎什么“共同托管”、“没有扩张野心”之类的奇谈怪论都冒出来了DD估计连罗斯福都觉得蛋疼了，所以才会反复询问。据说事后蒋介石琢磨着这事儿暑]里不对劲，&#12539;&#12539;#35810;王宠惠，王宠惠说琉球群岛很重要，应该收回。蒋介石责问说I&#65437;I&#65384;怎么当时不提醒我，王宠惠回答：“我作为委员长的部下，自然要与委员长保持一致。”蒋介石口吐鲜血，叮嘱王宠惠低调处理，大家假装从来没有发生过这件事。这就不难理解后来美国所R托管区的时候民国政府为何视而不见了。<br />
<br />
不管是重大行政失误也好还是怎么回事也好，蒋介石放弃琉球群岛的事实可说是序&#12539;#20991;无误。不仅是老蒋了，某台湾网友的考据文《为什么钓鱼台是日本的》（<a href="http://tw.myblog.yahoo.com/jw!ARR7CzOBSEbGZjQIIAbtkQ--" target="_blank">http://tw.myblog.yahoo.com/jw!ARR7CzOBSEbGZjQIIAbtkQ--</a> /article?mid=1582&amp;prev=-2&amp;next=-2&amp;page=1&amp;sc=1#yartcmt）内引用了I&#65382;岸大量的资料，充分说明了在蒋介石放弃琉球之后的二十多年时间里，无论是大陆还是台湾都根本没有将钓鱼岛视为自己的领土。例如1960年台湾内政部发行的地图里便没有包含钓鱼岛，1958年北京地图出版社出版的日本地图和1965年台湾国防部与地理学研究所合编的地图里干脆直接将钓鱼岛标作日本领土。《日人民报》也在1953年发表文章，一上来就称琉球群岛“包括尖阁诸岛”，最后又说琉球“是和日本人民争取独立、民主与和平的斗争分不开”的DD这就是为什么我们“神曙N而不可分割的一部分”会落到一个二战战败且军事力量受限的国家手中，它不是被人用武力抢走的，而是被我们自己像曙t曙Y一样随手丢弃了。这钓鱼岛可能以前还算是中国的，但如今它毫无疑问属于日本，而且可以想见，只要日本人的大脑不像中国人一样抽风，以后它也仍然将是日本的一部分。人家一不偷二不抢的，谁叫I&#65437;政府无能，只能自认倒丈B。<br />
<br />
I&#65382;岸本来一直没把钓鱼岛当回事，一直到1968年，情况有变，在钓鱼岛突然发现了海量石油。于是乎I&#65382;岸在同一时间完成了“地图开疆”的壮举，钓鱼岛也在一夜之间突然就他娘的“神曙N”起来了。这就好像I&#65437;穷困潦倒的时候人们都对I&#65437;敬而远之，有一天I&#65437;突然飞黄腾障U，于是人们又一个个恬不知耻的过来跟I&#65437;称兄道弟追忆过去了，I&#65382;岸政府所扮演的就是这么个势利小人的角色。由此I&#65437;也可以看出，政府成天宣传什么“领土问题诀不动摇、诀不妥协”，无非是骗骗老百姓，其实领土早就已经被他们明码标价了，暑]天卖掉了当然也无所谓。周恩来当年就有言：“我们不要在这里为它（钓鱼岛）争论了。毕竟，在地图上，它们是I&#65437;几乎找不到的几个小点。它们之成为问题，只不过是因为它们周围发现石油而已。”一个政府对待国土的态度都那么功利，对待国民的态度自然可想而知。<br />
<br />
想想这日本人也是倒了八辈子穷丈B，他们把我们不要的东西视为珍宝，结果态度决定一切，白捡了一个大便宜，谁知半道突然杀出I&#65382;个死皮赖脸的流氓。有人说我国“搁置争议、共同开发”的政策是软蛋外交，其实等I&#65437;署&#65419;清了事情的来龙去脉再回头一想，原来共同开发的提法简直就是缺德冒蓝烟，就好像日本人说：北京到底属于日本还是属于中国，目前尚有争议，但我们不妨搁置争议，共同开发。无怪乎日本不愿意就钓鱼岛问题展开谈判，这个问题对他们来说根本就没什么好谈的，I&#65437;敢派船过来，我就敢抓I&#65437;船长，爱咋咋地。<br />
<br />
我知道肯定会有人振振有词的说：国际政治本来就是这样，没有道德，只有利益！在一个能往婴儿渚C粉里掺三聚氰z&#65380;的国家，我对此并不感到w&#65381;讶。<br />
