=======转载的分割线===========<br />
<br />
关于目前尖阁诸岛事件，在中日I&#65382;国之间引起了不少的纠纷。<br />
<br />
领土和主权并不是“神曙N”的，我认为百姓民生和人命一定重于那些抽象概念。<br />
但是据国家法和近代的历史背景，尖阁诸岛还是属于日本主权之下的。<br />
博主作为一个日本民I&#65433;，对今次中方的主张表示抗议，<br />
