																											<div id="postmessage_844671" class="t_msgfont">以下转自大陆浪人的blog（<a href="http://blog.goo.ne.jp/dongyingwenren" target="_blank">http://blog.goo.ne.jp/dongyingwenren</a>），他也是转载的，文中部分字符由于兼容问题可能显示的不太好，不影响阅读：<br />
