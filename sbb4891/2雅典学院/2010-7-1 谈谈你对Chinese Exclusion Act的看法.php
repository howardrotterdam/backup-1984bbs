Chinese Exclusion Act<br />
<br />
Forty-Seventh Congress. Session I. 1882<br />
<br />
Chapter 126.-An act to execute certain treaty stipulations relating to Chinese.<br />
<br />
Preamble. Whereas, in the opinion of the Government of the United States the coming of Chinese laborers to this country endangers the good order of certain localities within the territory thereof:<br />
<br />
Therefore,<br />
<br />
Be it enacted by the Senate and House of Representatives of the United States of America in Congress assembled, That from and after the expiration of ninety days next after the passage of this act, and until the expiration of ten years next after the passage of this act, the coming of Chinese laborers to the United States be, and the same is hereby, suspended; and during such suspension it shall not be lawful for any Chinese laborer to come, or, having so come after the expiration of said ninety days, to remain within the United States.<br />
<br />
SEC. 2. That the master of any vessel who shall knowingly bring within the United States on such vessel, and land or permit to be landed, and Chinese laborer, from any foreign port of place, shall be deemed guilty of a misdemeanor, and on conviction thereof shall be punished by a fine of not more than five hundred dollars for each and every such Chinese laborer so brought, and may be also imprisoned for a term not exceeding one year.<br />
<br />
SEC. 3. That the two foregoing sections shall not apply to Chinese laborers who were in the United States on the seventeenth day of November, eighteen hundred and eighty, or who shall have come into the same before the expiration of ninety days next after the passage of this act, and who shall produce to such master before going on board such vessel, and shall produce to the collector of the port in the United States at which such vessel shall arrive, the evidence hereinafter in this act required of his being one of the laborers in this section mentioned; nor shall the two foregoing sections apply to the case of any master whose vessel, being bound to a port not within the United States by reason of being in distress or in stress of weather, or touching at any port of the United States on its voyage to any foreign port of place: Provided, That all Chinese laborers brought on such vessel shall depart with the vessel on leaving port.<br />
<br />
SEC. 4. That for the purpose of properly indentifying Chinese laborers who were in the United States on the seventeenth day of November, eighteen hundred and eighty, or who shall have come into the same before the expiration of ninety days next after the passage of this act, and in order to furnish them with the proper evidence of their right to go from and come to the United States of their free will and accord, as provided by the treaty between the United States and China dated November seventeenth, eighteen hundred and eighty, the collector of customs of the district from which any such Chinese laborer shall depart from the United States shall, in person or by deputy, go on board each vessel having on board any such Chinese laborer and cleared or about to sail from his district for a foreign port, and on such vessel make a list of all such Chinese laborers, which shall be entered in registry-books to be kept for that purpose, in which shall be stated the name, age, occupation, last place of residence, physical marks or peculiarities, and all facts necessary for the indentification of each of such Chinese laborers, which books shall be safely kept in the custom-house; and every such Chinese laborer so departing from the United States shall be entitled to, and shall receive, free of any charge or cost upon application therefor, from the collector or his deputy, at the time such list is taken, a certificate, signed by the collector or his deputy and attested by his seal of office, in such form as the Secretary of the Treasury shall prescribe, which certificate shall contain a statement of the name, age, occupation, last place of residence, personal description, and fact of identification of the Chinese laborer to whom the certificate is issued, corresponding with the said list and registry in all particulars. In case any Chinese laborer after having received such certificate shall leave such vessel before her departure he shall deliver his certificate to the master of the vessel, and if such Chinese laborer shall fail to return to such vessel before her departure from port the certificate shall be delivered by the master to the collector of customs for cancellation. The certificate herein provided for shall entitle the Chinese laborer to whom the same is issued to return to and re-enter the United States upon producing and delivering the same to the collector of customs of the district at which such Chinese laborer shall seek to re-enter; and upon delivery of such certificate by such Chinese laborer to the collector of customs at the time of re-entry in the United States, said collector shall cause the same to be filed in the custom house and duly canceled.<br />
<br />
SEC. 5. That any Chinese laborer mentioned in section four of this act being in the United States, and desiring to depart from the United States by land, shall have the right to demand and receive, free of charge or cost, a certificate of indentification similar to that provided for in section four of this act to be issued to such Chinese laborers as may desire to leave the United States by water; and it is hereby made the duty of the collector of customs of the district next adjoining the foreign country to which said Chinese laborer desires to go to issue such certificate, free of charge or cost, upon application by such Chinese laborer, and to enter the same upon registry-books to be kept by him for the purpose, as provided for in section four of this act.<br />
<br />
SEC. 6. That in order to the faithful execution of articles one and two of the treaty in this act before mentioned, every Chinese person other than a laborer who may be entitled by said treaty and this act to come within the United States, and who shall be about to come to the United States, shall be identified as so entitled by the Chinese Government in each case, such identity to be evidenced by a certificate issued under the authority of said government, which certificate shall be in the English language or (if not in the English language) accompanied by a translation into English, stating such right to come, and which certificate shall state the name, title, or official rank, if any, the age, height, and all physical peculiarities, former and present occupation or profession, and place of residence in China of the person to whom the certificate is issued and that such person is entitled conformably to the treaty in this act mentioned to come within the United States. Such certificate shall be prima-facie evidence of the fact set forth therein, and shall be produced to the collector of customs, or his deputy, of the port in the district in the United States at which the person named therein shall arrive.<br />
<br />
SEC. 7. That any person who shall knowingly and falsely alter or substitute any name for the name written in such certificate or forge any such certificate, or knowingly utter any forged or fraudulent certificate, or falsely personate any person named in any such certificate, shall be deemed guilty of a misdemeanor; and upon conviction thereof shall be fined in a sum not exceeding one thousand dollars, an imprisoned in a penitentiary for a term of not more than five years.<br />
<br />
SEC. 8. That the master of any vessel arriving in the United States from any foreign port or place shall, at the same time he delivers a manifest of the cargo, and if there be no cargo, then at the time of making a report of the entry of vessel pursuant to the law, in addition to the other matter required to be reported, and before landing, or permitting to land, any Chinese passengers, deliver and report to the collector of customs of the district in which such vessels shall have arrived a separate list of all Chinese passengers taken on board his vessel at any foreign port or place, and all such passengers on board the vessel at that time. Such list shall show the names of such passengers (and if accredited officers of the Chinese Government traveling on the business of that government, or their servants, with a note of such facts), and the name and other particulars, as shown by their respective certificates; and such list shall be sworn to by the master in the manner required by law in relation to the manifest of the cargo. Any willful refusal or neglect of any such master to comply with the provisions of this section shall incur the same penalties and forfeiture as are provided for a refusal or neglect to report and deliver a manifest of cargo.<br />
<br />
SEC. 9. That before any Chinese passengers are landed from any such vessel, the collector, or his deputy, shall proceed to examine such passengers, comparing the certificates with the list and with the passengers; and no passenger shall be allowed to land in the United States from such vessel in violation of law.<br />
<br />
SEC. 10. That every vessel whose master shall knowingly violate any of the provisions of this act shall be deemed forfeited to the United States, and shall be liable to seizure and condemnation on any district of the United States into which such vessel may enter or in which she may be found.<br />
<br />
SEC. 11. That any person who shall knowingly bring into or cause to be brought into the United States by land, or who shall knowingly aid or abet the same, or aid or abet the landing in the United States from any vessel of any Chinese person not lawfully entitled to enter the United States, shall be deemed guilty of a misdemeanor, and shall, on conviction thereof, be fined in a sum not exceeding one thousand dollars, and imprisoned for a term not exceeding one year.<br />
<br />
SEC. 12. That no Chinese person shall be permitted to enter the United States by land without producing to the proper officer of customs the certificate in this act required of Chinese persons seeking to land from a vessel. And any Chinese person found unlawfully within the United States shall be caused to be removed therefrom to the country from whence he came, by direction of the United States, after being brought before some justice, judge, or commissioner of a court of the United States and found to be one not lawfully entitled to be or remain in the United States.<br />
<br />
SEC. 13. That this act shall not apply to diplomatic and other officers of the Chinese Government traveling upon the business of that government, whose credentials shall be taken as equivalent to the certificate in this act mentioned, and shall exempt them and their body and household servants from the provisions of this act as to other Chinese persons.<br />
<br />
SEC. 14. That hereafter no State court or court of the United States shall admit Chinese to citizenship; and all laws in conflict with this act are hereby repealed.<br />
<br />
SEC. 15. That the words &quot;Chinese laborers&quot;, whenever used in this act, shall be construed to mean both skilled and unskilled laborers and Chinese employed in mining.<br />
<br />
Approved, May 6, 1882.</div>

							
							
							
							
