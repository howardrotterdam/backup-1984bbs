													<h2>至于谈话技巧，稍说几条</h2>
						
						
																											<div id="postmessage_542905" class="t_msgfont">第一、不提观点，只讲事实，而且是只讲对方可能有兴趣的事实。要以消极性为主导，不要去煽动仇恨。说笑话的人，不能自己先笑，也不能自己给自己的笑话评价为“极其可笑”，否则就失败了。说事的人也不能自己先愤怒。<br />
<br />
第二、那些什么事都替政府说好话的人，其实不难对付，你只要能把谈话进行下去，始终坚持第一条的方法，他最后肯定说不下去了。比较讨厌的是那些说“外国一样坏”、“只能这么坏”、“事实你也不清楚”的人，对此，我们的办法应该是：以对他不利的方式试图终止谈话。参见：<a href="https://1984bbs.com/viewthread.php?tid=40137" target="_blank">https://1984bbs.com/viewthread.php?tid=40137</a><br />
<br />
第三、对于非知识分子，要从贬低知识分子入手，尤其当你显然比他更接近知识分子的时候，这么干非常灵验。要准备几个砖家叫兽出洋相的典型例子，到处都可以说。对于知识分子，知识分子的知识就是面子，所以他根本就不可能当着你的面接受你的观点，这种情况下要非常小心，不要让他看出来你们之间有观点对立和知识差距，只要他觉得丢了面子，就一定会跟你作对到底。只能装作他什么都知道，什么都懂，跟他一起抱怨。知识分子仇视的对象，一般是无知识（即无真实的高等学历的）权贵，要拿他们说事。<br />
