													<h2>其实不论汉人还是维人，大家都是一个笼子里的奴隶</h2>
						
						
																											<div id="postmessage_148476" class="t_msgfont">转自饱醉豚<br />
六二六韶光惨案，是政府的民族政策导致的。<br />
<br />
汉人女工被强奸的事情在广东经常发生，但是由于作案者大多能够得到有效处理，很少酿成大规模群体事件。这次因为涉及到维族群体，而且传说是涉案者被政府释放而不受惩罚的事情重复发生之后，才导致大规模群体仇杀。政府说厂方已经证实强奸之事纯属造谣，但是这是政府的说法，大众的常识是：强奸案件需要公安部门立案侦查，并非工厂领导可以作出结论的。官方的另一个说法是被打死的维族工人只有2人，这明显和视频看到的不符合。更有宣传者到新疆传言斗殴者是酒醉而死，这种官方谣言已经不能欺骗任何人。<br />
<br />
在新疆，一些穆斯林公职人员和学生被禁止去清真寺礼拜，甚至有些人被禁止蓄须。他们的宗教信仰被限制，民族自治权几乎没有，这是事实。<br />
<br />
在东南沿海，到处是新疆小孩小偷，到处是新疆流氓团伙，这些人即使被抓，也很快放出来，政府的纵容导致新疆流氓团伙肆无忌惮，导致当地民众对新疆人极度反感，这也是事实。<br />
<br />
东南沿海的新疆流氓，最擅长就是把犯罪事件民族化，把一个维族人的犯罪问题上升为“民族问题”，而这一招往往奏效。耐人寻味的是，在新疆本地，一个维族人犯了罪，要是有人试图上升到“民族问题”，得到的结果是相反的，任何试图煽动“民族问题”的人必将受到严惩。<br />
<br />
上世纪八十年代，新疆流氓团伙就成为东南沿海城市的大害。据说一些地方公安采取的办法是：武警换上便衣，和社会流氓混在一起，参与对新疆流氓集团的械斗，当场打死打伤一批，让这些新疆流氓团伙消失。这个办法很不合法，也很不人道，但是得到了的当地居民的叫好，因为没有更好的办法打击新疆流氓集团。<br />
<br />
在中国，最高的准则不是民族和谐和民族平等、信仰自由，而是国土不分裂，民族不暴乱。为了“安抚”新疆维族，避免刑事案件成为民族暴乱的导火线，政府不惜采取包庇维族罪犯的办法，这在东南沿海相当普遍。但是这只会导致更多的流氓打着“民族”的招牌做护身符从事抢劫、盗窃活动。这只会损害维族人在汉人中的形象，更激化群体之间的矛盾。由于新疆流氓团伙的增多，政府也一直强调“民族”问题，造成了维族人在内地违法犯罪可以拥有不受处罚的特权这个既成事实。有一种“阴谋论”的说法：政府支持这些新疆流氓在内地作案，是为了故意损害维族人的形象，便于镇压新疆叛乱的时候得到内地汉人的支持。<br />
<br />
当地群众对外来的维族群体的仇视，实际上是仇视一个犯罪不受应有处罚的特权群体，而不是一个民族仇视另一个民族。正如他们仇视那些宝马撞人不受罚的权贵，仇视那些抢尸体制造自杀案件的人。这些年的群体事件，大多出于民众对特权阶层的仇恨，而这些特权阶层的背后，都是国家暴力。中国政府的民族政策，把流到内地的维族群众变成了这样一个特权团体，但是同时也剥夺了维族人本该拥有的信仰权和自治权。<br />
<br />
该给他们的要给他们：宗教信仰自由，民族自治。犯罪豁免权不是应该给他们，就绝不该给他们。<br />
<br />
中共政府的宣传机构一直在设法禁止宗教信仰的自由。不仅新疆、西藏有这问题，汉人也一样。由于中宣部的丰功伟绩，普通民众对其他宗教所知甚少，甚至有些人以为穆斯林不吃猪肉是因为猪是他们的祖宗。无知导致的种种误解，明显不利于民族和谐。同样，对藏传佛教和达赖喇嘛的扭曲，对轮子教义和对轮子信徒的妖魔化，都导致了制造仇恨的后果。可以说，很多仇恨都是政府的宣传机构煽动出来的，虽然政府一直禁止煽动民族仇恨，但是它自己客观上在煽动仇恨。不论是宣传上的扭曲，还是赦免新疆流氓的刑事犯罪，实际上都是在制造民族仇恨。<br />
<br />
现在的穆斯林大多数不是激进的原教旨穆斯林，他们能够跟世界各种文化和平共处。马来西亚还是英国殖民地的时候，马来人并没有因为他们的宗教信仰成为恐怖分子，相反的，那时候被认为是恐怖分子的，是马来西亚共产党，其中大多数是华人，他们到中国接受军事训练，回到东南亚搞共产主义革命，搞暗杀、埋地雷、纵火。<br />
<br />
现在的伊拉克，普通民众并没有因为伊斯兰信仰而敌视美国人。那种大规模的敌视，只能是萨达姆、金正日之类的独裁者操纵媒体煽动出来的。<br />
<br />
宗教问题和种族问题，在世界各地都是一个难题，只是有些国家处理得比中国更好一些。在马来西亚，我曾入住一家华人开设的酒店，每个房间都有指向麦加的箭头，而抽屉里放一本中英对照的《新旧约全书》。在新加坡，甚至在地皮昂贵的市中心，也有设立在大楼地下室的清真寺。在一些大商场和大学校园里，有穆斯林祈祷室。在中学里，孩子们可以选修宗教课程。在新马泰长大的华人孩子，绝对不会说出“猪是穆斯林的祖先”这类蠢话。如果在中国的学校里，少开一些《马哲》、《邓论》、《毛概》之类的垃圾，开设一些宗教讲座和各民族文化介绍，或许会更有利于种族和谐和宗教和谐。马来西亚的穆斯林，按照他们自己的法典审判，也可以一个男人娶四个老婆，并没有因此跟其他种族造成大的冲突。中国政府为什么就不能学习一点别人的经验呢？香港和台湾可以一国两制，难道新疆和西藏就不可以一国两制？<br />
<br />
在广东，虽然有很多维族人，但是你很难看到清真寺。我相信礼拜和祈祷对减少穆斯林的犯罪率是有效的。如果在广东多设清真寺，在有穆斯林的工厂和社区设立足够多的祈祷室，工作安排上允许他们一天几次祈祷，可以降低犯罪率。<br />
<br />
共产党人不善于利用宗教统治。他们的办法有两种：一是禁止宗教信仰的自由，二是让共产党员披上宗教人士的外衣去操纵宗教组织。《星岛日报》就报道过 2起这类事件：维吾尔族人包尔汉，1949年久入党，却一直保密党员身份。在耶鲁大学获得神学博士的李储文牧师，被红卫兵打到半死的时候居然摸出一个党证自保。后来此人担任上海革命委员会外事办主任，新华社香港分社副社长。<br />
<br />
与之类似的还有中华圣公会华北教区总干事、燕京协和神学院教务长、北京基督教三自爱国运动委员会副主席赵复三牧师，在1964年夏作为革命干部奉命调入世界宗教研究所，负责批判神学的任务，并在1980年代出任中国社会科学院副院长和党委副书记。天安门大屠杀后，赵复三背叛共产党。<br />
<br />
此外，傅铁山、赵朴初、丁光训之类的宗教界名人，也都被怀疑为打入宗教界的共产党员或共产党的“亲密朋友”。89年天安门大屠杀后，傅铁山是唯一一个跳出来声明支持屠杀的宗教界名流。荣毅仁副主席死后被证实是一个隐瞒共产党员身份多年的一个“托儿”。那些宗教界名人，不知道还有多少是永远隐瞒真实身份的党托。<br />
<br />
那些历史悠久的宗教，能够流行这么多年，说明他们是能够和统治者和谐共处的，否则早被统治者灭绝。但是共产党似乎缺乏和这些宗教和谐共处的能力。没有和党中央保持高度一致的思想统一，会让他们心神不宁。<br />
<br />
追求民主宪政而坐牢的刘晓波，签署《零八宪章》的人，被不断妖魔化的达赖喇嘛的支持者，被妖魔化程度更高的轮子信徒，以及地下基督教会和失去宗教自主权的维吾尔人，他们有一个共性――被剥夺了信仰自由和言论自由的人。<br />
<br />
造成626惨案的最大原因，不是参与屠杀的汉人具有先天性的暴戾，那制造强奸案件的维族流氓也不过是点燃了导火线，真正的元凶是中国政府的民族政策，正是这种长期执行的民族政策制造了种族仇恨这个火药桶。<br />
<br />
那些杀人的汉人，在这次冲突中表现得非常残忍。但是平心而论，他们长期以来是受害群体。在广东，几乎所有的政府部门都护着新疆人。镇压汉人群体事件不会有大问题，但是镇压维族人，会遇到很大的阻力。因此地方政府迫于当地民众压力清理新疆流氓的行动，有时候竟然要借用黑社会和便衣联手的方式。广东的新疆维族人不是是弱势群体，实际上，跟新疆维族人住在一起的广东汉人才是弱势群体。<br />
<br />
他们应该做的是去问责这个政府，但是他们也知道这是无效而徒劳的。这些被愤怒和正义感充满的人们，只能把仇恨发泄在那些他们可以对付的人身上，就是那几百个新疆人。我不能否认他们杀人的时候内心充满了复仇的正义感，但是这种正义感又是多么残忍而毫无理性。<br />
<br />
626冲突中那些被杀的人，大多数无辜的。但是在某方面，他们也是有过错的。在内地，每当一个新疆人犯罪被抓的时候，总有几十几百的维族同胞站出来为这些罪犯制造“民族事件”，从而把自己这个群体变得拥有更多的豁免权。当然这其中有一个重要原因是因为维族人在新疆长期受到的压制和政府对维族人的不信任，导致他们心理上上更敏感，而更容易不明是非地抱团。从效果上来说，这些人不仅在损害汉人的利益，也在损害维吾尔人的利益，正是他们自己的行为，导致了维族人被妖魔化，导致了一些汉人对新疆人的普遍仇恨。他们中的很多人都参与了支持少数新疆流氓的活动，都用实际行动损害维族人自身形象。用阴谋论的话来说，他们是政府妖魔化维吾尔族的助手。<br />
<br />
这次的626韶关冲突虽然很惨，但是跟历史上规模庞大的种族冲突相比算不了什么。既然历史上更大规模的政府组织的种族灭绝都没能导致维汉之间长期的仇恨，这些偶发性的民间冲突更不该成为两族人民冤冤相报的借口。<br />
<br />
维族人在新疆被边缘化，被限制宗教信仰自由和自治权，并不是普通汉人对他们做的。普通汉人和维族人一样，也被限制了宗教信仰自由，也没有任何意义上的自治权――他们到现在也没有选举统治者的权利，他们也跟维族人一样，不断地有突发事件反抗政府，也不断被镇压。<br />
<br />
当汉人仇视维族人，当维族人仇视普通汉人的时候，他们都容易忘记：其实不论汉人还是维人，大家都是一个笼子里的奴隶。</div>

							
							
							
							
