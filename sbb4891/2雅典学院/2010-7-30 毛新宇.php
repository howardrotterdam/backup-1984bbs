													<h2>毛新宇</h2>
						
						
																											<div id="postmessage_765994" class="t_msgfont">毛新宇（原创译文，想了解毛泽东唯一后人情况的请抓紧，估计会被和谐） 来源： 尹敏志的日志 <br />
（此文是华裔加拿大人Jan Wong的自传《Red China Blues》，Anchor Books版的序言。此书被时代杂志评为1996年的十大好书。因为我觉得国内关于毛泽东后人的报道太少太不准确，所以决定将这一片段翻译成中文。此文提供了一个西方人的视角，而且作者还是华裔，曾在北大读书，在中国待了几十年。错误之处在所难免。译文不代表本人观点。）<br />
<br />
 <br />
<br />
毛主席的孙子毛新宇是我见过的所有中国人中最胖的。23岁的他，圆脸、下颚和弥勒佛般的身材简直跟他爷爷一摸一样。差得远的地方是毛泽东蒙娜丽莎式的狭长双眼，和让人难以捉摸的微笑。他没有跟爷爷一样，把头发后梳得平滑发亮，而是竖起来，像把刷子。<br />
<br />
1993年，在离毛泽东的一百岁生日还差七个星期的某一天，我溜进了位于北京的军事医院来看毛新宇。之前我已经试了几个月，想采访毛泽东的后人，但是一直没成功。接着我得到消息说毛新宇在这里。手握一束黄玫瑰，我费了很多口舌，才通过了荷枪实弹的哨兵和三个关口的检查。<br />
<br />
毛新宇正在他的房间里看电视。我介绍自己是加拿大Globe and Mail的记者。“进来吧，”他说，带着灿烂的笑容，好像我们是老朋友。“这是京剧，能让我把它看完么？”<br />
<br />
他的房间里散发着臭袜子的气味。头发蓬乱，没刮胡子，肚子胀鼓鼓的新宇穿着医院发的条纹衣。他的裤子拉链只拉了一半，脚上穿着人字拖，所以我可以看到他灰色的尼龙袜上有几个洞。新宇遗传了他们家族不修边幅的习惯。他的爷爷毛泽东是个奇怪的人，30年代的时候，有一次埃德加・斯诺采访他，毛说着说着居然脱下裤子抓起虱子来了。毛有了大权，搬进了金碧辉煌的北京皇宫后，从不自己洗澡，全部由侍从在晚上用热毛巾擦拭他的身体。<br />
<br />
新宇出生于1970年，那一年，毛泽东的声誉达到了顶峰。对他来说这一切再正常不过了：到处是“红太阳把我的心照亮”的歌声，每个人都别着毛主席徽章，甚至有更狂热的信徒把毛主席像刺在胸口上。虽然毛泽东不是个好爷爷，但他还是化了很多时间给自己唯一的孙子起名字。伟大舵手当然明白一个好名字有多重要。“新宇”的意思是“崭新的宇宙”，意思是毛泽东做的一切都是为了创造一个勇敢新世界。<br />
<br />
毛泽东还有两个孙女，但按照中国老传统，她们是不能算作继承人的。新宇是唯一一个名字非同凡响的孙子。他在北京西边一个半山腰的村庄里被养大，有一打护士、司机、厨师、保姆、贴身保镖、武官照顾和保护他。没有玩伴和其他成年人。1976年，他的爷爷死了。当邓小平开始消除毛的个人崇拜时，新宇还是个小男孩。他父亲毛岸青此时已残疾，还得了忧郁症。他的妈妈始终紧跟着党的路线。<br />
<br />
当新宇还在学走路的时候，她的妈妈有时溺爱他，有时又惩罚他。她有时狠狠地打他，用得棍子都打断掉了。有时她又一个劲地给他吃糖、猪肉、油炸食品，希望他能长胖。但她始终觉得新宇不够胖，为此她焦虑不已，于是让她听话的私人医生给儿子注射了激素。<br />
<br />
毛泽东从没有抱过他、搂过他，从没陪他玩过。但新宇却很崇拜他的爷爷，用他知道的各种方法模仿他的爷爷。他最喜欢吃的菜也是伟大舵手的最爱――红烧肉，一种用大豆酱、茴香、米酒、红糖烧的带皮的大厚块猪肉。<br />
<br />
几年后，新宇的体重已激增到300磅（即272斤――译者注）。为了减肥，他什么都试，从减肥茶、草药到减肥霜。我见到他的那天，是他在军事医院307号病房强制减肥的第37天，在这家地铁西边的医院，他把体重减到了250磅（即227斤）。5英尺9英寸（即176厘米）的他，按西方标准来算，其实也没超过肥胖线多少。但在一个被他爷爷的乌托邦政策弄得很多人吃不饱甚至饿死的国家里，他还是很胖。<br />
<br />
他活在自己的白日梦中。有个大学学生问过他以后想做什么工作，他答道：“当领袖。”就好像当领袖跟当电工那样普通。他一直心怀这样隐秘的愿望：某人在某一天会选定他当中国共产党总书记。1989年时，他在中国人大。当TAM事件爆发，他的同学血洒广场的时候，她的妈妈让司机开车送她去学校，硬生生把自己儿子绑回家不让出门，直到风波过去。所以，他根本不知道解放军在广场上冷酷地射杀了普通学生，自然也没感觉到一点震动。<br />
<br />
我见到他的时候，新宇已经本科毕业了，现在在中央党校学毛泽东思想的博士学位。他梦想着出国。“我妈妈想让我去美国读书，因为那里有很多很多人在研究毛泽东思想。我听说在美国，毛主席的地位比乔治・华盛顿还高，是这样的吗？”<br />
<br />
“恐怕不是，”我回答。<br />
<br />
新宇好像没听到我的话。他叹了一口气，肚子微微晃动。一个密西西比州的大学――他忘了名字――愿意提供他一笔奖学金（可见，毛新宇的智商并不低，只是太喜欢沉浸于自己的世界中，这一点和他爷爷很像――译者注），但被中央禁止。“政府不会让我出国的，”他说道，“他们怕我出去了以后思想发生变化，或者就不回来了。”<br />
<br />
他继续看电视上声音刺耳的京剧。我问他打算在医院里待多久。“我不想回家，”他承认，“我不想让我妈告诉我该做什么。我怕她。这里时间过得很快啊，我能天天看电视。”<br />
<br />
毛泽东的孙子居然是个电视迷？毛泽东的孙子还想去美国学毛泽东思想？毛泽东的孙子被共产党囚禁？我的脑子一阵晕眩。毛泽东的王朝最后居然变成了这个样子。<br />
<br />
于是我开始回忆，刚到中国的时候，自己还是一个毛的信徒。<br />
