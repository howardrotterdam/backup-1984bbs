面，误导国际舆论。” <br />
<br />
　　他所列举的现象，可能是有的，但这同“革命”有什么关系呢？<br />
依我之见，情形正好相反。刘晓波是为了革命而匆匆赶去北京的吗？<br />
不是。四月十八日北京学生上街后，他还在《世界日报》发表一篇长<br />
文，对胡耀邦百般贬损，意思是胡耀邦算老几？不过是共产党的一个<br />
头头罢了，有什么值得悼念的？因而他本来是反对这场学生运动的。<br />
可是不出两日，他突然打电话告诉我，他决定回北京了，旧金山纪念<br />
“五四”七十周年的会议他不去参加了。日后我才从他身边的人获<br />
知，他那时见到有的人在国内已大出风头，急得他抓耳挠腮，坐立不<br />
安，这才突然决定归去的。临行之前，还不忘留下一个谣言：刘宾雁<br />
和一个什么人到美国人那里说刘晓波的坏话，反对他参加那个会议。<br />
一到北京，又造出一个谣言，说他是放弃了哥伦比亚大学访问学者的<br />
高薪毅然回国投身“革命”的。<br />
