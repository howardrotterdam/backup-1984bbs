找到一些线索。刘晓波比大学生更年长、更自信、更狂妄，因而在一<br />
般大学生身上处于比较稀释状态的那种极端个人主义和蔑视人民的思<br />
想和情绪，在刘晓波身上便更浓烈、更露骨和更疯狂。他在“末日”<br />
