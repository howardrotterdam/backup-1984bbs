种意见尤为尖刻，我刚刚听到时真如五雷轰顶。”<br />
<br />
　　郑义在香港同着名美学家高尔泰先生谈起此事。高尔泰告诉他：<br />
“你还记得在成都吗？我给你讲刘晓波最近来一信，说他不久又要一<br />
鸣惊人了。”郑义一算那时间──一九九一年秋，他们夫妇匿居高尔<br />
泰家中，恰恰是“末日”成书之前。郑义来美后，又向诗人江河谈及<br />
此事。原来刘晓波在纽约卖给江河一本“末日”时，曾向他明白无误<br />
