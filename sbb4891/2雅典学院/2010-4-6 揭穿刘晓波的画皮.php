虽未正面回答，似乎对刘晓波的这种做法很是不以为然。后来又从郑<br />
义先生的文章中得到证实。郑义提到刘晓波“末日”一书的前言中的<br />
一段话。刘写到有的朋友读过此书的反应是：“你的忏悔尽管读起来<br />
颇有震撼力，但这是不是一种更高级、更巧妙的自我解释和自我辩<br />
