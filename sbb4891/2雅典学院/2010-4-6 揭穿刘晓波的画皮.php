言与诬蔑的书和录影带，已经出了几部。第二个，便是大名鼎鼎的刘<br />
晓波。刘晓波写了一本书，叫《一个末日幸存者的独白》，后来又写<br />
了一篇文章，《我们被我们的‘正义’击倒》，发表于一九九三年八<br />
月台北的《中央日报》。刘晓波不同于中共，他参加了天安门运动，<br />
又是在运动尾声中开始绝食的所谓“四君子”之一，还坐过牢，因而<br />
他的记述和分析的可信性，会被中外读者认为比中共的宣传高出许多<br />
倍。<br />
<br />
