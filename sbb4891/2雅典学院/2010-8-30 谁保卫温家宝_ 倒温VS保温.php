													<h2>谁保卫温家宝?     倒温VS保温</h2>
						
						
																											<div id="postmessage_803789" class="t_msgfont">原文：<a href="http://www.yzzk.com/cfm/Content_Archive.cfm?channel=ae&amp;path=2268621452/35ae1a.cfm" target="_blank">http://www.yzzk.com/cfm/Content_ ... 68621452/35ae1a.cfm</a><br />
<br />
中国总理温家宝在深圳讲话高调推政治体制改革，表明政治理念，反击各派对他的攻击；传统左派一直猛烈抨击温家宝，视其为「赵紫阳集团」的领军人物。 自由派作家余杰、权贵势力也对温家宝看不顺眼，冷嘲热讽他为「影帝」。 支持温家宝的不但有中共体制内开明人士，也有自由派知识分子，崔卫平等人称温家宝为体制内上升的力量、民主的力量，指出他的深圳讲话堪比当年邓小平南巡讲话。<br />
<br />
<br />
作为中共领导下的中央政府，国务院总理相当于「CEO」，既要瞩目于技术层面的操作，又要以政治家的担当，修补共和国大厦的裂痕与空洞，他更是精神导师，要鼓舞十三亿民众的信心。 多重身份，一样情怀。 今年六十八岁的温家宝，二零零三年三月出任总理，尚有埙年半任期。 二零一零年一月，美国欧亚集团评选出新的一年最值得关注的全球十大领导人，温家宝排名第一；最近，美国《新闻周刊》选出十名赢得各界尊重的国家政府领袖，温家宝获赞扬为「人民公仆」。 被众多中国人昵称为「平民总理」的「温爷爷」，对他的评价，在中国国内却引起截然不同的两种观点的激辩。 有人激烈「倒温」，有人热诚「保温」。<br />
<br />
八月二十日一连两天，在深圳特区三十周年前夕，温家宝前往深圳考察，这是他出任总理以来，第八次考察深圳特区。 他说，「只有坚持推进改革开放，国家才有光明前途」，「不仅要推进经济体制改革，还要推进政治体制改革，没有政治体制改革的保障，经济体制改革的成果就会得而复失」，「违背人民的意志，最终只会是死路一条」。 这一番激昂言论，已引起海内外舆论的广泛关注。 他在讲话中高调强调政治体制改革，更以空前语句反击传统「左派」，即保守派的抨击。 令人不解的是，在深圳电视台的报道中，这些话却不见踪影。 人们纷纷从各个角度揣摩温家宝讲话的背景和涵义。<br />
<br />
就在温家宝考察深圳的消息披露后的翌日，传统「左派」大本营的「乌有之乡」网站，旋即发表宇太的《我反对的是不怀好意的改革》、陈中华的《建议中国停止错误的改革政策》、《改革不是对国家和民族进行温水煮青蛙》等文章。 宇太的文章说：「我反对的是错误的改革，是不怀好意的改革，是打着改革幌子掠夺人民共有财富的改革，是假以改革谋求自家先上天堂而不惜把人民推向地狱的改革。有一些所谓精英，实际是叛国叛民者，意欲以改革为手段，搞垮中共，搞垮人民，以便达到彻底搞垮中国之目的，为美国献上一份超级大礼……凡私欲熏心者，不足以领袖也，不足以解救苍生也，不足以谋天下也。怀念毛泽东，中国属于人民。」这些宣言式的文章，影射温家宝「不足以领袖也」。<br />
<br />
近年来，在中国政坛和民间社会，特别是网络上，谴责温家宝的风声不绝于耳，猛烈抨击温家宝当总理八年来的政绩与政策，把温家宝视为「赵紫阳集团」的领军人物。 这股批判潮流是怎么形成的，对中共十八大中南海领导层，乃至中国发展的未来趋向有何影响，这是人们所关注的。<br />
<br />
令人瞩目的是，被视为自由派的作家余杰，也在香港出了一本《中国影帝温家宝》的书痛批温家宝（见二十六页专访），成为「倒温」声浪中三股势力之一。 这三股势力一是中国国内称之为「极左派」、「保守派」的毛泽东思想左派；一是权贵势力，即特殊利益集团；一是对温家宝「只说不做」看不顺眼的自由派人士。 按理说，这三股政治力量走的原本就不是一条路，一个反对当今的经济体制改革，更不愿启动政治体制改革，企图在中国恢复毛泽东时代；一个致力于维持现状，阻碍改革继续深化，以保护特殊利益、既得利益；一个是呼吁推进政治改革，捍卫普世价值，却认为温家宝只会「作秀」，没有行动，不配当总理。 他们以不同的立场，将矛头指向温家宝。<br />
<br />
当下，「倒温」逆流已经是第四波了。 第一波是二零零七年三月，温家宝提出「普世价值论」之后；第二波是零七年十月中共十七大至零八年三月第十一届全国人大一次会议，中共和政府班子换届前后；第三波始于二零一零年春天，二月中央春节团拜会上，温家宝提出让人民活得「更有尊严」，三月，在十一届全国人大三次会议上，温家宝再度重提「人民尊严论」；当下，温家宝在青海地震、甘肃泥石流以及考察深圳的讲话，激起第四波「倒温」潮。<br />
<br />
二零一零年以来，温家宝突然展现了与以往不同的风貌。 他连续多次，以不同的方式，诸如春节前与网友的交流、春节团拜会上讲话、全国两会政府工作报告、两会后答记者问、《求是》杂志撰文、在灾区和高校的言谈、在深圳视察的谈话，宣扬社会公平与正义，倡导人民幸福与尊严，要推动政治体制改革，甚至宣称「我的时间不多了」，「在我在任的最后几年，我将为这件事情尽最大的努力」，这既向国人坦露他的紧迫感，宣布今后的施政方向及重点，也因其高标独立而很不寻常，让人欣喜之余也颇感惊讶。<br />
<br />
勇猛果决的反击<br />
<br />
如果联系到近数年来社会上和执政集团内部一股势力掀起的倒沩潮，对温家宝宣示的普世价值的猛烈而持续的批判，可以想见这是温家宝在隐忍多时后的一个旗帜鲜明而果决猛烈的反击。 此举有「虽千万人吾往矣」的勇敢，却也似乎是破釜沉舟、有进无退的最后一搏，表现出「正其义不谋其利，明其道不计其功」的悲壮。<br />
<br />
早在零六年九月，温家宝在接受五家媒体采访时就说过：「民主是人类共同追求的价值观和共同创造的文明成果。」在零七年二月他再次强调了民主等普世价值。 零八年他在美国接受CNN采访时说：「我相信我们在推进经济体制改革的同时，也要推进政治改革。因为发展是全面的，所以改革也应该是全面的。」至于批判温家宝，根本原因也在于他赞成普世价值，主张政治体制改革。 这些讲话无疑强烈地触动了那些政治体制改革反对者的神经。<br />
<br />
二零一零年八月上旬，中国社会科学院召开二零一零年专题研讨会上，全国政协副主席、中国社会科学院党组书记、院长陈奎元讲话指出，在苏联垮塌以后，社会主义的理想及发展模式，几乎威信扫地，「在许多人的眼里，美式资本主义是完美无缺的」，是「历史的终点」。 这种观点在中国理论界、社会科学界特别是青年人中的影响也非同小可。 「在思想理论界有些人产生崇美的心理，认为社会主义国家只有完全改弦更张才是出路，这种思想倾向的影响不容低估」。 「美国的领导人将中国称之为『利益攸关方』，而不是称作朋友或伙伴。他们不会放弃遏制中国持续发展的战略，中国不能一厢情愿做美国新帝国主义的诚实伙伴」。<br />
<br />
在两年前的七月至十月，陈奎元就借批判普世价值之名含沙射影、指桑骂槐。 他在多个场合一再强调：「过去基督教宣扬其教义是普世价值，现在西方话语权的声音高，把他们主张的『民主观』、『人权观』以及自由市场经济理论等等也宣称为普世价值，我国也有一些人如影随形，大讲要与『普世价值』接轨」。 「我们要树立民族自尊心和自信心，不搞任何盲目崇拜，不能将西方的价值观念尊奉为所谓的普世价值」。<br />
<br />
二零一零年六月四日，被网友推举为「乌有之乡」旗帜的中央民族大学教授张宏良，发表《千古兴亡亡于一相》，把两三千年的中国历史，描述为「宰相亡国史」 ，以古喻今，影射国家带来最大危险、威胁民族命运和国家前途的，是当朝宰相――国务院总理温家宝。 五月下旬开始，北京政坛和学界流传前中共中央政策研究室综合局局长张勤德《对温家宝总理的六条意见》一文，将温家宝树成最大靶子，公开挑战现行政策。 这篇写于零九年九月的文章，给温家宝扣上了「中国特色资本主义思想得到更大解放」，「资产阶级自由化改革观产生更大影响」，「庸俗发展观造成更大危害」，「卖国求荣的犯罪行为更严重地发生」，「赵紫阳集团的气焰更加嚣张」，「人民群众的根本利益受到更大损害」六顶大帽，甚至提出关于温家宝太太与儿子从商的各种传言，要温家宝澄清。<br />
<br />
二零一零年四月十五日，温家宝在《人民日报》上发表了纪念胡耀邦逝世二十一周年的文章《再回兴义忆耀邦》。 于是，不少人发表文章毁谤胡耀邦，把胡耀邦说成是「中国诸多灾难形成的标志性人物」，借否定胡耀邦，打击温家宝。<br />
<br />
零八年秋冬，署名李必胜的《人人都应提问和回答的七大问题――对三十年改革「十大失误」原因和责任的追问》一文，在京城流传。 这篇文章把改革开放以来所出现的种种社会恶疾，全算在温家宝的头上。 是年十一月十七日，中国社会科学院马克思主义研究院副院长侯惠勤，在北京《中国社会科学院报》发表的文章也指出：「把资本主义文明看做是不可超越的终极存在，是『普世价值』热播者的意识形态前提；把当代中国的改革开放纳入资本主义世界文明的轨道，是他们热衷传播『普世价值』的根本目的」。 「今天我国某些人大谈『普世价值』，既表明了其挑战社会主义主流意识形态而又信心不足的虚弱本质，又表明了其臣服于当代西方强势话语而又极力加以掩饰的矛盾心态」 。<br />
<br />
是年九月十六日，北京《光明日报》刊登了中国人民大学马克思主义学院教授周天城《一些人鼓吹的普世价值实际就是西方的价值》。 九月十日，《人民日报》转载《马克思主义研究》第七期冯虞章的文章《怎样认识所谓「普世价值」》。 八月二十九日，《北京晚报》发表题为《虚伪的「新闻自由」与媒体的社会责任》一文，批判普世价值……<br />
<br />
中央党校教授杜光说，他们直指「赵紫阳集团是当前最危险的敌人」，「眼下我们面临的斗争焦点，是如何防止和反对赵紫阳集团」，不点名地把温家宝封为「赵紫阳集团的新头子」。 赵紫阳和胡耀邦是八十年代改革开放的带头人。 把温家宝看做「赵紫阳集团的新头子」，一方面反映出这些人反对改革的本性，也说明温家宝受到他们攻击、诽谤的真正原因，是他坚持了改革开放的路线。<br />
<br />
打击温家宝架空胡锦涛<br />
<br />
有学者认为，「倒温派」把责任推到温家宝头上，其醉翁之意，是要清算赵紫阳主张政治改革的总帐。 他们为主张政治改革的人，创造了一个名词「赵紫阳集团」，这个「集团」包括任仲夷、李君如、杜导正、杨继绳、谢韬、杜光、李慎之、周瑞金、高尚全、吴稼祥、茅于轼、徐景安等人，而这些人的总代表就是总理温家宝。 「倒温派」把所有当今中国社会、经济、政治的弊端，统统算到温家宝头上，目的在于：为当前的中国经济倒退、金融危机、官员腐败、贫富悬殊、民怨沸腾、社会动荡，寻找替罪羊，为特权阶层解脱责任；倒温派否定赵紫阳政改主张的最终目的，是要影响改革的道路和方向。 他们自称是社会主义改革派，他们说的改革必须坚持四项基本原则，即坚持一党专政。 陈奎元说，他决不赞同那种脱离党的基本路线，背离人民利益的那种改革。 杜光认为，「批判普世价值的人所反对的，不是普世价值这个概念，甚至也不是自由、民主、平等、人权这些价值理念；他们所反对的，是根据这些价值理念来设计和建设的制度。他们反对按照自由、民主、人权等价值理念来改革政治体制和社会体制。这才是问题的本质所在」。 他们表面上是拉胡倒温，实际上是透过打击温家宝来架空胡锦涛，这已是他们的主要策略原则。<br />
<br />
在二零零九年，是国际金融危机冲击下，中国国民经济增速「保八」的一年，是自然灾害频发的一年，温家宝三十六次考察，足迹踏遍中国内地。 二零一零年过去了大半年，不少网民感动于六十八岁的温家宝表现的勤勉热忱和中国士大夫传统。<br />
<br />
有网民写道：「为中国民众有这么一位『心系苍生，爱共黎庶』的领导人而庆幸。作为一国的行政首脑，你花甲已过，活得太累，随时随地要担忧各地的发展、安全、稳定，担忧突发事故，担忧百姓吃不起饭，穿不上衣，过不上年。你是务实的总理，从电视看到你，颤微的脚步，嘶哑的嗓子，忧愤的心，为一个农妇承诺追讨欠薪，在井下与矿工一起吃饺子，为地震遇难者流泪，向小学生遗体三鞠躬。当今世界，还没有哪位国家领导人能像你那样，把政治权力阐释得如此温情脉脉。」<br />
<br />
也有网民说：「在中国做官，有很多种做法，温家宝选择了人民希望的做法。他一句句大白话，大实话、大真话，在中国官场上不多见，却在民间流传。西方有言，总统是靠不住的。我知道，好总理不如好制度，但好制度也要好总理来推动。」<br />
<br />
前国家新闻出版署署长、现任《炎黄春秋》杂志社社长杜导正，八月二十五日接受采访时，一提到温家宝，便一再用一串形容词称赞他。 杜导正说：「据我了解，胡锦涛是很支持温家宝的，近年在一些重要问题上，一直放手放权挺总理。赵紫阳生前也对我说过，『温家宝这个人很好，胡锦涛是个明白人』。我认为，应该给温家宝更多的权力，施展他的才华，这对国家有利，对百姓有利。保护温家宝，不是维护一个人，而是维护一个主张，维护一个力量。」<br />
<br />
杜导正说：「我和我身边的一批老朋友，对温家宝的评价很高很高，他是真心实意地、坚决地贯彻中共十一届三中全会路线，理解邓小平做的那一套，他是坚定地、鲜明地、不屈不挠地在行动上实践改革开放的，他从来没有动摇过，不仅表现在言论上，就是在行动上，恐怕现在中央最高级领导人中间，他是旗帜最鲜明的，他也是最有功劳的。他的言论很多，文章很多，很多高层都不敢说这些话，他却敢说。他是赵紫阳、朱F基一脉相承的一位很优秀的领导人。他在作风上，更是中国共产党人那些传统的东西，非常亲民，非常务实，非常感动人，大雪、地震、洪水，他第一时间出现第一线，与非典病人、艾滋病人握手。他也是活人，他也是一条生命，却不考虑自己，他一贯如此，不是作秀。他很有学问，记忆力又极好，我认为，他的表现，是以他的学问和传统为基础的。」<br />
<br />
杜说：「温家宝是中国当政者中，一个很优秀、很优秀、很优秀的人物。如果让我投票，我把第一张票投给他，我这话不是代表我一个人，我周围一大批朋友，对他评价都很高。古今中外，优秀人物往往被人诽谤，反对的力量有时出于某种目的造谣生事。我希望，境外希望中国进步的力量，都能维护他，保护他。当然他也是活人，人无完人，会有缺点和错误。至于他家属的问题，根据我们的了解，温家宝是个纯粹的人，他本人绝没有问题，外界对家属的一些议论，真真假假，温家宝家属可以出来正面澄清，哪些是真的，哪些是假的，哪些讲的是有道理的，哪些是他制止不力。日前，邓小平长女邓林在深圳接受媒体访问时，就澄清外界多年来的传闻：邓家没有海外帐户和存款。这样就很好，政治不透明，往往会被一些人利用而攻击他。」<br />
<br />
中国社会科学院研究生院博士生导师、前《人民日报》副总编辑周瑞金说，在中共十八大召开前，中央九常委中，温家宝总理最鲜明最具体地代表中央发出毫不动摇推动政治经济体制改革，坚决防止改革开放停滞倒退的声音，联系到他明确肯定政治文明的普世价值，深情回忆胡耀邦，这必定会在海内外引发广泛关注。<br />
<br />
周瑞金认为，在中国现代化建设取得举世瞩目的重大成就情况下，温家宝最冷静地从社会主义初级阶段的长期性和完善社会主义制度的艰巨性战略高度，指出中国各方面体制机制还不完善，中国特色社会主义还不成熟，因此要坚定不移地推进改革开放，清除影响经济社会发展和人的全面发展的各种体制障碍，以最大限度解放和发展生产力，建成完善、成熟、有中国特色的社会主义。<br />
<br />
周瑞金说：「温总理反覆强调政治体制改革的重要性、必要性，最直接具体地提出政治体制改革的目标。」当下「有相当一部分人沉醉于『中国模式』的盲目乐观之中，温总理却慧眼特具看出中国改革开放有停滞甚至倒退的危险。因此这次借深圳之行，大声疾呼，要头脑清醒，明辨是非，停滞与倒退不仅会断送三十多年改革开放的成果和宝贵的机遇，违背人民的意志，最终只会是死路一条」。 ■</div>

							
							
							
							
