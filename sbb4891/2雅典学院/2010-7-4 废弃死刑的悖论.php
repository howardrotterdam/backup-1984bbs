													<h2>废弃死刑的悖论</h2>
						
						
																											<div id="postmessage_720064" class="t_msgfont">在普遍相信血亲复仇或者类似道德观有很大影响的社会中，强行推广废除死刑，就会有可能面对这样一种悖论。引子就是那个日本未成年人杀婴案，被害者家属说，如果判罪犯无期徒刑（实际由于是未成年人，很快就会放出来），那不如现在就把他释放，然后让我亲手杀了他。<br />
<br />
既然法律不能以死刑制裁杀人者，那么当被害者以私刑处死杀人者后，报复者又成了杀人者，这个时候法律同样不能以死刑处置私刑杀人者... 由此递归，直至反复私刑报复之下，一方彻底死绝为之。<br />
<br />
要跳出这个怪圈，法律只能以确实有效的彻底执行终身监禁这类惩罚，让一个人同社会彻底隔绝，这个时候，对社会的影响，终身监禁其实就等同于“死刑” -- 让他人间消失。事实上，即便如此，在血亲观念深重的社会里，仍然无法避免被害一方对加害一方亲友的私刑报复，父债子偿这类道德观念在中国仍然根深蒂固。法律除非能同时保护加害者的亲友免于私刑报复的危险，否则仍然无法跳出这个怪圈 -- 中国的传统意识形态里，从来没有个人的观念，任何一个人的行为实际上都是以家族为承载体的，连做，株连九族这种东西能长期存在，都同这种意识形态有直接关联。<br />
<br />
还有，如果只考虑社会效益的话，让一个人消失，直接处决无疑要比终身监禁成本小的多 -- 废除死刑是个好东西，但<br />
<br />
1）意识形态相对“落后”的社会环境玩不得；<br />
2）纳税人不够富有也玩不起；<br />
<br />
这两个关键因素不解决，废除死刑永远都是空中楼阁。废除死刑是文明的象征，是每个人努力的目标 -- 但这不等于说你废除了死刑你就文明了。<br />
<br />
