																											<div id="postmessage_689599" class="t_msgfont">中国：不遵守规则的世界 -- -- 看完后 太震撼了 校内上还没见过 绝对的好文章 所以就转来了 大家也看看 (原创）群体犯罪心理――就在你我之间，当代国民群体的劣根性浅析 来源： 高浩津Gavin的日志 <br />
在我们对西方世界了解得太多时，却对自身了解得实在太少，乃至于一切的憧憬与蓝图都是建立在一个不能融合的土地上。<br />
 <br />
<br />
首先我来讲个故事，这是发生在几年前的“安利退货门事件”，故事发生在中国经济最活跃的城市――上海（本人也是上海人)：<br />
<br />
自九十年代后期，随着跨过企业进入中国，西方世界与中国才在基本的生活层面真正开始相互接触。在跨国企业大批量迁往中国的过程中，一家名叫“安利（Amway）”的美国保健品跨国公司，也希望在这片它并不熟悉的土地上开展蓝图。<br />
<br />
作为一家排名世界500强、并且是前三十名的国际知名企业，安利公司直销制度体系显得非常独特，并且被世界首富比尔盖茨另眼相看，形容为“最无懈可击的激励制度”、被哈佛MBA和中国人大MBA列为教材案例，这家公司自然是实力雄厚，对中国市场充满了期待。<br />
<br />
然而，正是这家巨型企业，在中国最繁荣的城市上海，领略到的是东方人的不可思议之处：<br />
<br />
刚进入中国的安利，一切制度是以它在欧美的设计为标准。按美国安利规定，产品实行“无因全款退货”：不管任何原因，如果顾客在使用后感到不满意，哪怕一瓶沐浴露用得一滴不剩，只要瓶还在，就可以到安利退得全款――注意哦，是退全款！这项制度在美国施行了很久，一直是安利公司的信誉和品牌象征，退货率微乎其微(估计那&quot;微&quot;也是在美国的中国人)，安利的产品是优质的。然而在中国，精明的国人很快以“特色”的方式震撼了美国人：很多中国人回家把刚买的安利洗碗液、洗衣液倒出一半，留用，然后再用半空的瓶子、甚至全空的瓶子去要求全额退款。在上海，刚刚开业不久的安利公司，每天清早门口排起了退款的长长队伍，络绎不绝，人潮涌动，一时间，令安利的美国人大吃一惊。<br />
<br />
美国人怎么也搞不明白：作为拥有半个世纪经营经验、一整套完整制度体系的安利企业帝国，他们“全额退款制度”在西方实行一直良好，为何到了中国，竟然遭遇如此数量巨大的退货？真的是产品质量不好吗，以致于引起成百上千的中国老百姓要求退货？<br />
<br />
但由于承诺在先，安利还是顶着每天的巨大亏损，忠实履行了退货承诺。然而，令人更加惊异的现象发生了：一方面是产品销售量剧增，大大超乎公司的预期；可另一方面，拿着空瓶子前来退货的顾客也越来越多，最后竟然达到每天退款高达100万元，还得倒贴30万元产品――终于让美国安利吃不消了！从这之后，安利公司迅速对中国的制度进行修改：产品用完一半，只能退款一半；全部用完，则不予退款！自此，安利（中国）改变了其公司制度，转变了原先安利（美国）的营销模式，开始逐步领悟“中国特色”。<br />
<br />
傻乎乎的美国人被精明的中国老百姓耍懵了，上海市民们在这场“退货风波”中或许暗自冷笑，为自己得到的小便宜而沾沾自喜。很多美国人至今也搞不明白：为什么他们被哈佛大学引以为豪的最先进的制度体系、被誉为“完美无懈可击的一整套激励制度”，在中国这片神奇的土壤上，竟遭遇滑铁卢般的惨败？<br />
<br />
中国人，真是太奇怪了，这完全是一个不遵守游戏规则的世界。<br />
<br />
在已故的美国作家阿伦特、法国社会心理学家勒庞的笔下，现实社会中有一群这样的人：群氓。他们并没有犯什么伤天害理的罪行，为的只是图自己的小便宜、或是盲目从众，而最终的结果却是导致了整个社会群体的混乱、更大的丑恶，对整个社会造成极大的损害（信用损害、道德损害、物质损害――如果我们倒退回30年前，是不是能找到似曾相识的情景？），而真正的罪魁祸首却无法从他们当中找到，因为他们每个人并不是大奸大恶。阿伦特因此也称之为“平庸的邪恶”。<br />
<br />
事实上，这种“平庸的邪恶”现象普遍发生在我们中国民众身上，大大小小的事件，层出不穷，就在你我周围，也就是你我每个人心照不宣的小伎俩。尤其，发生在中国最发达的城市、最具有现代公民素质的城市――上海，这样一件当年轰动的“退货门事件”，无疑是给国人自己打了一记重重响亮的耳光！<br />
<br />
透过这个经济层面的现象，我们发现的是一个令人尴尬的中国现象：即使是西方最优的制度和文化（被哈佛与世界首富认可），到了中国，就陷入泥潭，不仅不能有效实行，反而被国人给“特色化”、同化了。 <br />
<br />
记不太清楚是谁（可能是新加坡总理李光耀），曾经说过一段话，大概意思是：任何制度的设计、哪怕是世界顶尖学府和精英设置的体系，都经不起中国人的糟蹋，因为中国人是最精于钻空子的，无孔不入，即使是堪称完美的制度也仍然防不胜防，所以，唯一的办法就是――用专制手段。<br />
<br />
这段话大概是这个意思，一直在我脑海中印象深刻。当然，这样的话会令今天我们所有有良知、有现代公民素质的中国人感到愤怒，会刺激我们大多数网上的愤怒青年们。我们的社会在发展，我们已经是全球不可忽视的力量，能够左右世界经济格局，我们应该有条件拥有最完善的制度、应该达到美国那样的社会憧憬。怎么可以说，我们没有资格具备现代公民的素质呢？<br />
<br />
然而事实或许恰恰正是这样：我们确实仍然是一群群氓！并且，这种群氓心理在中国社会的各个角落，无孔不在。<br />
<br />
我有几位大学同学，在政府部门的县、镇一级做公务员，多年的好友，只要有时间，平常电话、网络常常联系，都能与他们交流一些东西。有一位同学就说，现在基层的问题多如牛毛，事情不大，却整天像苍蝇一样嗡嗡地难以处置，上也不得，下也不得，上不敢得罪，下也不敢得罪，而当地老百姓的一些作为更让他哭笑不得，理想被现实的无奈取代。<br />
<br />
他说的有一个事件很值得让我深思：一个外地货车，运的是某种食用油（大概是吧，我是听说的），行至到该乡村的崎岖道路时，因为路面不平，翻了车，货袋破了，黄油流了出来。司机急的是团团转，不知如何是好，这时，该地的村民们出现了，一个、两个、三个、四个……越来越多，司机心想：这下有救了，有人帮忙来了！谁知这些村民们个个拿着袋子，并不是来救援，竟然是冲上前装油，一袋、两袋……装得满满，喜滋滋地拎回家去！司机惊得目瞪口呆，却又无可奈何，拦不住，而村民们更加有恃无恐，甚至去扯那些本没有破的货运袋子，把完好的口袋全撕破了，汩汩的油流出来，不长时间，一整车的货就分到了这些村民手中，他们一个个喜形于色，仿佛白白捡了天大的便宜，却把那司机气煞得是怒火朝天，却无计可施。<br />
<br />
货运车不是一辆，后面跟着来的司机们全都怒了，联合起来找当地政府，要求赔偿，惩罚那些“刁民”。政府倒是挺重视，派人前来处理，可村民们不答应，死活不肯把黄油还给司机，双方发生冲突，有人员损伤。闹到后来，村民们不肯善罢甘休，大骂政府是吃里扒外的东西，向着外地人，要求政府必须对村民们赔偿精神、损伤。于是乎，这件令人无奈而两边不是人的事件，最终还是让当地政府做出让步，对两方都进行安慰，自己掏腰包补偿双方，才得以消停。<br />
<br />
该同学是当事人之一。他在大学时，也曾是愤世嫉俗的青年，侃侃而谈天下大事，动辄自言“以后我要是当政了就如何如何”之类话语，如今在基层干了两年，当年的意气风发早已不见，有的只是无奈的苦笑，说：罢了罢了，在中国，就是这样……想必再多过几年，这样的事再见得更多，也就心态麻木，睁一只眼闭一只眼，任它风雨欲来，我已岿然不动了。<br />
<br />
农民们辛苦，但并不一定善良；农民们不容易，但他们同样愚昧无知。对于当代的知识分子来说，我们似乎天然地把农民这个群体当作值得同情的对象，但我们恰恰没能够真正深入他们的生活，了解到他们思维的本质。这些“群氓”们在自己田间地头、自己狭小地盘上，同样为了争夺利益而相互内斗：为占小便宜而损人利己的群体思维模式、农村里为争灌水溉田而相互拆台、为争山林而两村人大打出手、为了点蝇头小利不惜偷盗电线放火烧山、还有最为频繁的地方利益两伙村民相互间、动辄就是扛锄头群殴不怕事小、或是张家长、李家短然后恩怨相互往死里整……总之，鲁迅的一句话：可怜之人，必有可恨之处。<br />
<br />
中国地域差距之大，现象可谓千差万别。上海的市民们疯狂退货的举动、与田村老农们疯狂抢货的举动，在我看来，却并无二致，恰恰是映照了我们这个时代――或者说是千百年来一直未变的国人群体心理。我的眼前浮现出的是这样的群氓――无论是在高楼大厦的繁华城市、还是在穷乡僻壤的山间田边，他们都为着自己心里的小算盘、小利益做精确的打算，想着的是那点便宜，如何才能最快、最有效地到达自己手里？<br />
<br />
说到这里，我又想起一个中国特色的现象：领取退休金。<br />
<br />
中国老一代人们的退休，通常是从自己单位、机关里领取退休金，而由于老人们的行动不便，这种按时领取退休金的行为通常由子女们代领。而在中国，许多老人去世之后，子女们不主动通知原单位、并且继续以老人的名义领取养老金的现象普遍存在，有新闻报道曾有老人去世十年后，其子女依旧在以老人的名义去领养老金。事实上，这种“群体贪小便宜”的现象在中国早已是公开的秘密，全国大大小小成百上千城市、乡镇，不知有多少老人的子女们在钻这样漏洞。乃至于传出某市镇的单位，大呼退休金不够发，不得已要求退休老人必须拿当天报纸亲自拍照片，证实本人还活着，才能领取养老金――当然，这引起老人们的子女群体的抗议，认为是对人格的歧视。而在领取退休金的背后，却通常是人们潜意识里的心态：反正是国家的钱，既然没阻止我领，也没发现，我为什么不领？我这不算贪污犯罪吧？<br />
<br />
中国有成千上万个城市、乡镇和单位机关，这种公然在老人死后、继续以老人名义领取退休金的中国特色方式，数目之多，倘若真的一五一十严查起来，恐怕严重得惊人。这其中，数量上占多数是平头百姓的退休职工，他们的子女为贪图老人的几百元退休金，年年月月如此，尽管他们个体认为区区几百元并不算违法，但这种“群体犯罪”的可怖性恰恰展现了中国人内心的劣根。这让我想起了八十多年前的鲁迅所写的《再论雷峰塔的倒掉》一文，一个有着千百年历史背景、西湖十景之一的文物建筑――雷峰塔，因为老百姓们传说此塔的砖块搬回家可以“辟邪”，于是纷纷偷挖雷峰塔的砖头，你一块，我一块，纷纷搬回家，按人民群众的说法是：我就搬一块砖头，不算犯法啊。于是，最终，雷峰塔在这样“集体无意识犯罪”的行为下，轰然倒塌，尸骨无存！<br />
<br />
这，才是我们的人民的真实本质。 <br />
<br />
这种思维的普遍性，深入每个人的内心，不论是高高在上的官员，还是普通的平头百姓，只要任何人得到了钻空子的机会，就会不择手段地去捞取最大利益，并且，他们并不认为这是可耻，而是心安理得地接受这一切、身体力行地去为之去“争取”。<br />
<br />
现实社会中的许多现象，给予我们一个巨大的问号：难道制度真正能够解决所有问题吗？对于精明算计的国人来说，恐怕任何完美的制度都依然能被钻漏洞，所有的体制都约束不了国人。<br />
<br />
当这个时代的人们在高呼“完善体制”时，我恰恰认为，制度是不能解决一切问题的，其实质的问题首先在于“人”。同样是乘坐公交车，在美国为了鼓励人们出门坐公交，规定凡乘坐四十五次以上（无人监督），就可申请领取一定的奖金，美国人都很遵守这条规则，而不少中国人却借此虚报次数，以获取奖金；在澳洲，有一条规定“不得擅自从国外寄食品、或不知名中草药物进入澳洲，否则将严厉查处，如果难以查到国外的寄件人，就会对该国国内的收件人进行罚款”，澳洲人从未想过这条规则有何漏洞，而有中国人却为了整别人，竟故意寄违禁品去澳洲，利用该规则，以使收件人受到惩罚。<br />
<br />
同样的制度，同样的环境里，美国、澳洲可以遵守，而国人却绞尽脑汁地寻找漏洞。对此，我们该做如何解释呢？<br />
<br />
鲁迅笔下，雷峰塔的轰然倒塌，归功于每个“百姓”的功劳，每个人贪一点小便宜，抱一块砖回家，造就了一个悲剧。而如果全民参与这种“群体犯罪”的话，恐怕倒掉的就不只是一座雷峰塔那么简单。<br />
<br />
我不禁要问：谁说人民就是善良的、无辜的？<br />
<br />
如果说那些借老人名义领取退休金的子女们，贪的只是一点点小钱。那么在曾经《南方周末》所报道的一则“重庆市民假结婚骗取房子”的新闻中，我们看到的是平民百姓们更加触目惊心的“群氓”行为：<br />
<br />
重庆市的一个小镇――人和镇，竟然在2005年创造了世界上最不可思议的离婚记录。这个人口仅有2万人的小镇，短短一年时间里竟有1795对夫妇离婚，然后是假结婚、假再婚、复婚。这种滑稽的群体表演，原因在于重庆市的一份征地补偿办法规定：一、一对夫妻只能分一套房，但离了婚单独立户，就可以各分一套房，并以优惠的价格购买；二、配偶为城镇户口且无住房，可以申请多分配一间屋，从一室一厅变为一室两厅。<br />
<br />
如此优惠、良好的保障制度，在中国这片神奇的土壤上，再一次遭遇类似“安利退货”般的嘲弄，这再一次让我质疑：究竟是制度本身的问题，还是这个社会群体出了问题？<br />
<br />
据《南方周末》报道，该镇的老百姓无论年龄大小，纷纷踊跃加入离婚队伍。“村里老太爷老太婆都来离婚了”，“七八十岁走不动路，儿孙扶着来的、背着来的都有，一大家人，有说有笑地排队”。面对如此离婚热潮，高新区管委会副主任徐南雄很无奈地说：“55号令规定离婚分户可分房，新婚姻法又简化了离婚程序，我们没有理由去阻止离婚。”<br />
<br />
而对于大多数村民们来说，离婚只是第一步，接下来是更加复杂的任务：再婚。于是人们又一次行动起来，最关键的是找人。村民们发动一切亲朋好友，在政策划定的“老重庆9区12县城镇户口”范围内寻找结婚对象。村民们甚至开始“悬赏”，赏价从最初的六七千元一路飙升到上万，重赏之下“丈夫”和“妻子”源源不断涌来。<br />
<br />
这样的事例我不想再说了，在我们这片特色的土地上，一切皆有可能。没有办不到，只有想不到，国人对于体制、环境的超强适应能力，还有极具特色的现实想象能力，真是令人惊叹叫绝而又不可思议。<br />
<br />
历朝历代，百姓们通常会大骂贪官、大骂权力垄断者，因为那些人往往有比我们更顺捷的渠道，去获取本不应得的利益。我们常常说：官是黑的、丑恶的，而民都是无辜的、善良的――其实这是自欺欺人的说辞。在“安利退货”、“农民抢黄油”、“退休老人子女冒领退休金”这些发生在平民百姓身上的事情中，我没有发现“人民”的任何善良之处，恰恰相反，我发现的是：普通百姓只要一旦获得了某种权力（哪怕是临时的），有机会更改游戏规则时，他们表现出来的群体犯罪行为、集体窃取，与那些贪官、腐化分子的作为没有两样，甚至表现得更明目张胆，更加令人可怖，因为――每个人都堂而皇之地认为自己无错。民与官，不过是同属一个共同的群体罢了。而这个群体，就是我等诸位国人。 <br />
<br />
当阿伦特在用其笔调描述“群氓”的现象时，她是否知道，这个词语真正、彻底地适用于我们这个国度。<br />
<br />
个体的行为，孤立来看，并不足以产生多么严重的影响，而一旦这个群体的每一个人都如此的话，将会汇聚成为一股洪流，产生极大危害。对于国人来说，不可能有真正的公平公正。倘若有一天，我们真能施行一个公平公正的体制、或是自上而下的进行全面改造，那么我相信，这一切终究被国人的小便宜、小利益、小团体心理所取代，渐渐演化的仍然是颇具中国特色的你我之间的算计、抢夺，乃至亘古不变的“窝里斗”、“相互拆台”。<br />
<br />
在今天国人的思维里，1966-1976这段延续十年的历史事件一直是国家与民族丑陋的伤疤(文化大革命)。作为每一个平头百姓的个体，都有理由对这段历史充满了义愤填膺般的愤恨。几乎所有的人民都将其归罪于国家的领袖，认为这完全是个人集权下的恶果――这种说法尤其盛行于今天40-60岁这一代人当中，他们今天成为了社会的主导群体，自然有了对过往历史的评价权。<br />
<br />
然而事实恰恰不是这样。如果不是国人群体本身就拥有惊人的破坏力，如果不是因为人们相互之间本就有猜忌、嫉妒、互整的传统心理，如果不是我们这个民族千百年来积累下来的群体犯罪思维，我很难相信，仅仅凭借某个人的号召，就能掀起如此大的一股浪潮？国人的“选择性失明”的劣根性再一次暴露无遗：恰恰是40-60岁这一年龄阶段的人们，在当年那场延续十年的事件当中，正值他们青春岁月的愤怒青年时代――正是他们，主演了这场悲剧，他们举一反三地发挥自己的破坏力和想象力，将整个民族的劣根性进行了一次经典的登场演出。<br />
<br />
回想当年，这些20多岁的年轻人，与今天许许多多的网上的愤怒青年一样，高呼“革命”、“革新”、“造反有理”，对现行制度的一切充满了愤恨和挑衅，他们目中无人、见人就咬。这个群体对上级、长辈的反叛，演化成从“文斗”的大字报、“破四旧”，发展到“武斗”的打砸抢、抄家，红卫兵秉持着“激进的就是正义”的狂热信仰，对国家机关、政府体系进行破坏性的冲击，尤其在对待那些当年打江山的老革命、老同志时，这些年轻人把他们内心的狠毒表现得淋漓尽致：肆意踢打、辱骂，剃成癞痢头以示人格侮辱，戴高帽、挂贴牌进行游街，私闯民宅抄家，肆意没收他人财产……这无异于一场大规模群体犯罪，而犯罪的人们竟然将此称作“正义”。发展到后来，就是砸教堂、捣庙宇、挖坟墓、焚烧文化典籍、毁坏文物古迹，以后的受害者逐步涉及到工商业者、上层民主人士、名作家、名演员、中学教师――而在这其中，红卫兵们的内部又演化出不同的势力代表，“造反派”、“极左派”、“保守派”，他们相互间争权夺利，大打出手，俨然是整个社会以“正义红旗”的名义，变成了黑社会式的江湖世界。<br />
<br />
中国式的“群氓”们无比狂热，群体犯罪的丑恶心理下，是比“安利退货”、“搬倒雷峰塔”严重数千倍的社会损伤。<br />
<br />
我们需要真正反思的，是这样一个问题：为什么领袖的三言两语，就能轻而易举地调动起整个群体的狂热？――这根源恰恰不在某个人的威信力量，而在于这个群体本身就拥有着恐怖的群体犯罪心态。这是一场规模浩大的“中国群氓们”的历史性演出。而作为民族的最高领袖者，无疑是洞悉了民族心理的这种丑恶心态的。只不过，领袖负责打开了这个潘多拉魔盒后，魔盒里的所有卑劣人格、心态都一涌而出，就再也控制不住，延续达十年之久。<br />
与其说是领袖引导了民众的行为，倒不如说，这是民众们集体无意识的利用领袖的言辞、来达到人民内心深处隐藏的目的。当年的红卫兵、红小兵们肆意抄家、打人、凌辱他人的人格时，往往借助的是领袖的语录，一句“毛主席教导我们”就足以掩盖他们自身的邪恶。我的外公、爷爷当年都曾被游街示众，而将他们打倒、羞辱的那些红卫兵们，恰恰是当年那些他们用尽心血培养的青年干部。与今天的社会现状类似，在那个年代的这群年轻人心里，对老一辈人充满了不信任，在这些青年眼里，那些老顽固思想落后、早已被糖衣炮弹的“资产阶级”、“里通外国”所腐化，他们正是“祸国殃民”、“残害群众”的罪魁祸首――怎么办？红卫兵们，兄弟姐妹们，都响应起来吧，“革命无罪，造反有理”――革那些老顽固的命、打烂他们的旧世界、抄他们的家、把他们游街示众、让他们受尽折磨！――这，就是“群氓”们心底最真实的本质，群体犯罪的最经典的一次上演。<br />
<br />
人们常说，有什么样的领袖，就有什么样的国民。我看来恰恰相反，应该是有什么样的国民群体，才会产生什么样的领袖。领袖不过是看透了民众们的内心世界，洞悉了他们的所思、所想，于是，领袖们的轻轻撩拨，就足以把这个群体引导到一个狂热的状态。与之类似，德国的纳粹思潮、日本的军国主义，都无疑彰显了这样一个事实：领袖可以更换，但群体思潮依然狂热，人民的群体思维代代相传。<br />
<br />
红卫兵的主体正是当年20岁左右的年轻学生，正是这群愤青而无知的学生，在今天已成为40-60岁的社会中坚力量。随着改革开放、经济的发展，这个年龄段的人们自然而然拥有了经济上、社会舆论的话语权，这伙当年“群体犯罪”的人们，没有丝毫的忏悔与反省，今天依旧用谎言自欺欺人，把历史的罪责推到某个个人的身上，却把自己打扮成无辜受害的模样――这个群体的犯罪心理，与那“安利退货”、“农民抢黄油”、“搬垮雷峰塔的百姓们”一样，竟然表现得心安理得、理所应当！他们并不认为自己做错了什么，反而认为自己的行为是大众中的一员：既然大众都这么干了，我一个人又需要承担什么过错呢？<br />
<br />
中国有句名言：法不责众。这正是我们这个民族的最大丑陋之处。一个人犯错，遭到千夫所指、过街喊打；而一群人犯罪，则是理所应当，集体无意识地掩饰。<br />
<br />
