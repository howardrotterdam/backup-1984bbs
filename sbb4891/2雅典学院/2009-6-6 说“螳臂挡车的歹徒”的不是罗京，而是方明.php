<br />
得乌烟瘴气，中央人民广播电台里也是一团乱麻。作为播音员的方明在得到这条讣告将由他来播时，心里充满了<br />
<br />
对广大群众的内疚。这是一条多么令人悲痛的消息呀，而将这条消息带给全国人民的不是别人正是自己。但这还<br />
<br />
不是最令方明窝火的。当时因为在录这条消息时，方明想着周总理在世时的点点滴滴，想着他在这样一个不利于<br />
<br />
自己的时期里还不忘保护其他的同志，悲从心起，不由得又掺杂进了自己的情绪。但这时的他还是比播陈毅同志<br />
<br />
的讣告时多了些经验，所以还是尽量从一个客观的角度来播报，可有人在听了后还是不满意：“不要哭哭咧咧，<br />
<br />
要化悲痛为力量。”方明愤然了：你不难过难道也不让别人难过吗！直至今天，方明在说到这时语气还禁不住激<br />
<br />
愤起来，他说：“我不得不难过，因为中国的命运是什么，人民的前途是什么？我找不到！要化悲痛为力量，那<br />
<br />
力量的支点在哪儿？我更找不到！” 转眼到了1997年，改革开放的决策者邓小平同志离开了我们大家，离开了<br />
