<br />
在这条讣告里他用了比较磅礴的气势，比较颂扬的语调。说起邓小平，方明一脸的敬意，崇敬之情溢于言表。他<br />
<br />
感觉心里是踏实：“因为小平同志一系列的拨乱反正，因为小平同志的改革开放路线，也因为小平同志的高风亮<br />
<br />
节，使我在伤心的同时看到了中国的前途，中国人民的希望。” ・两个选择：家庭和事业我永远选择后者 方明<br />
<br />
那极富魅力的嗓音通过那高挂在电线杆的大喇叭传遍了大江南北，无论是农家小院还是边防大寨，人们对中央人<br />
<br />
民广播电台这些播音员的声音熟悉得如同自己的家人一般。自然，求爱的信件也似雪片般的向方明飞来，可方明<br />
<br />
却说自己当时忙得连谈恋爱的时间都没有。其实是因为事业心太重的方明给自己定了一个原则:业没立，家不成<br />
<br />
！就这样，直到1972年，由齐越老师牵线，方明才开始认真地考虑自己的个人问题，结识了自己的老伴。可结婚<br />
<br />
后，夫妇二人又都为了奔事业，达成了一个共识:不要孩子。但这个由不得他们，虽然老的很开通，没有说什么<br />
<br />
