<br />
走。”这番话，方明说得斩钉截铁。儿子还在读中学时，有一次告诉老爸自己不舒服，想去医院。可想着公事的<br />
<br />
方明却挥了挥手说，还是先上学去吧，不就是感冒吗，吃两颗药就好了。后来，幸亏孩子自己去了医院，没人知<br />
<br />
道他是怎样一个人去的，当到了医院时他已经快站不起来了，医生见状立刻展开了急救，后来被诊断为心肌炎，<br />
<br />
