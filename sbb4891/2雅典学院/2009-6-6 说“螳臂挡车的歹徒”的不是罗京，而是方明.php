<br />
流言蜚语的攻击。“气不过”的方明于是决定:好，那我们就给他们生一个！就这样，1978年，儿子呱呱坠地。 <br />
<br />
儿子的降生并没有将方明和家庭生活拉得拢一点。事业和家庭仿佛是永远不可能协调的两个点，两难中而方明又<br />
<br />
往往选择前者。从小学到大学再到工作，儿子从没靠上这个可谓事业有成的老爸半点好处。照理说帮儿子是天经<br />
<br />
地义的事，可方明说他办不到：“我每天播的都是很正面的东西，号召大家要实事求是。如果我那么做的话不是<br />
<br />
