<br />
“这么年轻，冻坏了怎么播音呀！”要说老师的身体怎能跟年轻人比呀，就是这种无私的关怀，让穿上毛裤的方<br />
<br />
明感到了从未有过的温暖，因为这温暖来自心底。 ・一次大错：稿件的内容应该永远放在第一位 1960年方明开<br />
<br />
始了播音工作。工作不久，方明就出了一次大错，这次教训至今让方明记忆深刻，也让一向对自己要求严格的他<br />
<br />
现在都还不能释怀。1962年，罗荣桓同志去世，当时的讣告是由夏青直播的。那是一篇五千多字的稿子，夏青同<br />
<br />
志不但一个字都没有播错，并且整场下来，感情、态度、分寸、火候都十分到位，速度慢慢的，声音低低的。这<br />
<br />
让年轻的方明羡慕不已，不禁问自己：“我什么时候也能播得像他一样好呢？”想法有了没几天，机会就来了。<br />
<br />
一次他和雅坤直播新闻，有一条这样消息：“罗瑞卿同志从罗荣桓同志的家属手里接过罗荣桓同志的骨灰盒，安<br />
<br />
放到罗荣桓同志的灵位上。”方明一看，这可巧了，这不是向夏青老师学习的好机会吗，于是暗自在心里摩拳擦<br />
<br />
掌起来。轮到他时，他极力地在脑海里回想夏青老师的状态、语气、音调。没料到播着播着方明的脑子乱了：“<br />
<br />
罗瑞卿同志从罗荣桓同志的家属手里接过了罗瑞――”在“卿”字送到唇边的一刹那，方明清醒了过来，此时也<br />
<br />
已被吓得一身冷汗。话是接不下去了，他只得把这句话重新播了一遍。 按道理说这不过就是一个口误，可那个<br />
<br />
年代是不会这样认为的。当时的政治空气空前的浓烈，出了这样一个差错那还了得。就这样，大会检讨小会检讨<br />
<br />
，后来多亏领导还比较理智的看待这件事情。此时的方明也自责不已，决定从事情的根本去找原因：“仔细的分<br />
<br />
析来分析去，我发现我犯了大忌。作为一个播音员，第一位应该考虑的是稿件的内容，而不是什么形式，所以说<br />
<br />
不出错是偶然的，出错是必然的。”总结了教训，以后的工作方明暗自努力，也就有了几次机会。 ・三次讣告:<br />
<br />
从“悲愤”、“悲痛”到“悲壮” 1971年，陈毅老总去世，方明的心中悲愤不已。陈毅同志作为当时的中共中<br />
<br />
央委员、副总理兼外交部长、元帅，一个连国外友人都十分景仰的人，却在“文革”中被迫害至死。方明只觉得<br />
<br />
怒气在心口越积越深，久久不能散去。而且在那种政治高压下，有气不能撒，有话不能说，但播报讣告的工作还<br />
<br />
得自己来做。怀着对元帅的痛惜之情，方明越播越激动，到最后宪全不能抑制住自己的愤怒，将一腔悲愤融进了<br />
<br />
