<br />
了。看着病床上儿子苍白的脸，方明终于心软了一次，把大部分时间都留在了医院。那一次住院，也成了儿子记<br />
<br />
忆里和父亲待得最久的一次。 然而，就这样惊心动魄的一件事还不是方明最遗憾的，他最遗憾的是没能见上父<br />
