自己受益匪浅，在潜移默化中他学到了很多知识，也受到了艺术的熏陶，对他以后的播音工作帮助很大。 ・老<br />
<br />
师教我体会“学习要靠自己” 也许老天爷是有安排的，不会让方明读新闻的声音永远只展现在同学面前。两年<br />
<br />
后，机会来了，方明调到了播音室，正式开始了他与播音的不解之缘。 走上播音岗位后，在齐越、夏青等老师<br />
<br />
手把手的带领下，方明很快地融入了工作。那时的齐越和夏青可谓播音队伍的旗帜和楷模，在他们身上，方明学<br />
<br />
到了很多。如果说一般人能认识五千字就差不多了，可作为播音员，八九千上万的字都要掌握。但如果有不懂的<br />
<br />
字想问老师，往往会吃个闭门羹。一向有求必应的老师手朝字典一指:自己查！因为只有这样字才能记得牢。这<br />
<br />
是对方明最大的影响:他学会了自己学习。 在工作上，老师比学生还上心。节目播好了，他比学生高兴；节目没<br />
<br />
播好，他更生气。在生活上，老师也很尽心。方明回忆说：“我们当时住单位集体宿舍，男生宿舍一般都是脏、<br />
<br />
乱、差的代表。齐越和夏青老师经常自己带着扫帚、拖把来寝室给我们做清洁。”方明接着说:有一年冬天，齐<br />
<br />
