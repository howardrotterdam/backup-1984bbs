																											<div id="postmessage_854527" class="t_msgfont">讲这句话的人首先就没有澄清“中国”这个概念，什么是中？有学者认为，现代政治概念上的“中国”最早也只能推到《尼布楚条约》的签订才初具雏形，严格意义上甚至更晚。如果是传统意义上的“中国”则有着更多的不确定性：甚至在三国时期，孙吴称曹魏为“中国”，显然就是将自己作为“中国”的他者来对待。上古时期的“华夷之分”则是政治与文化概念兼具，因而什么是“中国”这个问题，即便是在古汉语的语境中也是难以澄明的。<br />
<br />
其次，“文化”的概念无法澄清：在孔子的时代，当下意义的“中国”正处于四分五裂的春秋战国时期，各地的文化风俗差异还非常明显，如果说儒家所谓身体发肤受之父母，那么越人断法文身的习俗肯定是不同的了。孔子的“祖师”称号之于越人如何？之于为周天子放马的秦人如何？之于擅自称王的楚人如何？<br />
<br />
秦国依仗虎狼之师统一列国，建立书同文、车同仑等等政策，这才从政治意义上第一次统一了这个国家，而“书同文”更是解决了“中国文化”的最基础问题：语言。如果不是书写语言的统一，中国历史的发展很难不说会有更多的可能。从这个意义上，秦王嬴政才是中国文化的最重要的奠基人。这不是很讽刺么？一个集权者、杀戮者、文化的摧毁者，同时又奠基了这么一个怪异的中国文化，这就是儒家所需要的答案吗？<br />
<br />
显然为了解决这种悖论，必须改变关于“文化”的根本观念，即文化不是由几位大师拍拍脑门造出来的，而是文明的一部分，与这片土地上每一位居民都息息相关，是一个有机的成长过程，是一种习惯、风俗达到一定规模后形成的社会共识，最终达到了一种想象共同体。那些还在寻找“大师”的人，干脆掘个坑跟大师们埋在一起算了。</div>

							
							
							
							
