																											<div id="postmessage_854734" class="t_msgfont">或曰：“儒家是世俗学说，现在中国又没有传统的士大夫阶层，知识分子不足以对当权者形成制约，所以儒家的解释权实际掌握在官方手里。官方很容易借公祭孔子的机会，把天地君亲师那一套鬼把戏扯出来当遮羞布，这比中国传统的专制社会还糟糕。我认同徐先生的看法。”<br />
<br />
答以两点。<br />
<br />
一、中国目前确实“没有传统的士大夫阶层”，但如果儒家受到真正的尊重（意识形态层面、宪法层面的尊重）这个问题就迎刃而解了。“马家”当道”，马克思主义学者成群结队，儒家在“位”，儒家学者自然也会大量出现。<br />
<br />
二、基督教和马列主义由于经典本身的问题，不用歪曲，其中大量的偏差、错误乃至邪恶思想就足够专制主义（神权专制或党主专制）利用了，再一歪曲更不得了。而儒家不一样，极具经典的道德性和原则规定性。儒家经典的文明先进宽容程度，不仅基督教和马列经典望尘莫及，就是佛经道藏亦有所逊色。<br />
<br />
仁义原则（等同于中庸原则、良知原则）为儒家的基本原则，其下还有很多子原则，如民本原则、和谐原则、以直报怨原则等等。因此，即使官方实际掌握了儒家的解释权，必然要受到儒家经典和原则相当程度的制约；即使官方试图歪曲儒家，也不是那么容易的。<br />
<br />
别小瞧“天地君亲师那一套”，它已经创造了数千年优先于世界的文明辉煌。尽管需要某种与时俱进的升级，但比起马列主义和基督教那些“鬼把戏”来，已是正确高明得不可以道里计。时代不同了，“君主”理所当然要换成“民主”了，这正是儒家“时”和“义”两大原则的要求。在仁本主义的导向下，只要有实践机会，“天地民亲师”这一套必将创造中华民族新一轮的文明奇迹。<br />
<br />
另外，“儒家是世俗学说”，更是道德学说、政治学说，同时具有相当的宗教性----良知信仰不是宗教胜似宗教。且不说儒家如何高明精微怎样优异，在不违科学常识而富有科学精神方面，基督教那种“神圣学说”就应该害点羞了吧，呵呵。2010-10-4东海儒者余樟法</div>

							
							
							
							
