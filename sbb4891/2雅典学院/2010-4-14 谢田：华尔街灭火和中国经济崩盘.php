&nbsp; &nbsp; 这位专家明显不懂得经济学。克林顿政府时期，美国经济一直强劲，美元自然就强硬。奥巴马政府以来，美国经济一团糟，失业率高达10%以上，美元自然就要贬值。难道你要求美国经济一团糟了，还要货币强硬？这符合经济规律吗？<br />
