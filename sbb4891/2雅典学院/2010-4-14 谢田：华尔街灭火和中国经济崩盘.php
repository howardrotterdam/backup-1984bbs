													<h2>红色警报──泡沫专家解读中国投资狂热</h2>
						
						
																											<div id="postmessage_562499" class="t_msgfont">2010年3月23日，总部位于波士顿的美国投资公司GMO发表泡沫专家钱塞勒(Edward Chancellor)撰写的研究报告《中国的红色警报》。报告指出，中国经济已经显现出显著的投机狂热特征。300年以来历史上出现的著名经济泡沫，其形成与破裂的经验凸显出中国经济目前的脆弱性。<br />
现代人很可能会嘲笑那些在17世纪抢购郁金香球茎的荷兰人和18世纪狂买股票的法国人、英国人。但是，人们可能也还记得20世纪60年代的“新股狂热”、70年代的“最优50股股票热”、80年代的生物技术泡沫、日本股价和地产暴涨以及90年代初的暴跌，还有90年代后期的“因特网狂潮”。历史警示我们：人们总是在重复过去的错误。 <br />
<br />
钱塞勒的报告分为两个部份。在第一部份中，作者列出了识别投机狂热和金融危机的问题清单。在这份清单中，钱塞勒列出了过去300年来著名“投资狂热”的10大特征。<br />
<br />
第一部份：识别投机狂热和金融危机－－300年来著名泡沫的10大特征<br />
<br />
1、巨大的投资灾难往往始于一个令人信服的增长故事。<br />
<br />
2、对当局能力的盲目信任是另一个典型狂热的特征。<br />
<br />
3、投资的普遍增加是金融灾难的另一个指标。<br />
<br />
4、腐败激增总是与巨大的繁荣相伴而来。<br />
<br />
5、货币供应快速增长是金融不稳定的一个可靠指标。<br />
<br />
6、固定汇率国家往往产生不合理的低利率，催生繁荣但终以泡沫破裂告终。<br />
<br />
7、信贷疯狂增长一段时间危机即来临。<br />
<br />
8、道德风险是巨大投机狂热的共同特征。<br />
<br />
9、金融结构变得不稳定是严重问题。<br />
<br />
10、快速上升的房地产价格。<br />
<br />
报告的第二部份将这10个因素应用于分析中国的现实。作者认为，诸多指标显示，中国已经出现了投机狂潮和金融脆弱的典型症状。<br />
<br />
第二部份：解析中国梦－－一个经济增长的旁氏骗局<br />
<br />
钱塞勒的分析指出，过去几年中在全球经济放缓的情况下，促使中国经济继续快速增长的几个因素如投资热潮、信贷激增、货币供应量大增，道德风险和高风险借贷等等，都是催生泡沫的经典要素，但是投资者和主流媒体认为他们可以放心地忽略这些，人们似乎普遍认为，“先建设，一切都会随之而来。”因为北京需要保持快速增长，忘了这种失衡可能带来巨大的负面影响。这实质上就是一个庞氏（欺骗）元素的循环。钱塞勒的分析凸显了以下一些事实：<br />
<br />
1、钱塞勒认为，中国的GDP增长已不再是经济过程的结果，而是成为了政府的目标。中央政府制订GDP增长目标，并分配给地方政府。地方政府反过来糊弄中央。古德哈特定律指出，每当经济指标成为政策目标，它就失去了信息的作用。地方政府的许多决定，无论是开展新的基建项目和发展房地产，都是以实现增长为目标。无论是政府还是企业，当资源分配的目标是为了一串数字，不好的结果是可想而知的。<br />
<br />
2、投资者对中国的经济增长预测抱一种不加批判的态度。比如，有预测声称中国的城市人口到2025年会再增加3.5亿，达到接近10亿。但中国目前的城市化率很可能被低估了，因为按照西方的标准，很多中国乡镇已经城市化。更复杂的是，许多从农村搬到城市的移民因为没有居住证，可能没有纳入官方的统计数字。人均GDP是地方政府官员的一项考核指标，所以他们有动力低报人口数字。而且，许多新到城市的人没有户口，收入微薄，而且在过去10年的实际工资没有增长。正如中国人口学专家学者所说，“将城市人口增长等同于中产阶级的人数增长，是片面的。”<br />
<br />
3、2009年，中国固定资产投资增长了30％，对去年经济增长贡献达到90%。投资创记录的上升到GDP的58%。基础设施投入占去年经济刺激开支的三分之二以上。大约四分之一的项目是政府主导的。许多项目明显只是为了满足政府的GDP增长目标。<br />
<br />
4、许多行业过度投资。欧盟商会的一份报告指出，去年11月生产能力过剩的包括造船业、平板玻璃、钢铁、水泥、多晶硅、风电。例如，在水泥行业资本支出增加了三分之二，但产能利用率只有78％。<br />
<br />
5、投资效率每况愈下。在1994年一篇著名的文章中，“亚洲神话的奇迹”诺贝尔奖得主，经济学家保罗．克鲁格曼认为，亚洲猛虎般的经济增长依靠不断增加资本投入。然而这种投资，依然受收益递减规律影响。这一分析同样适用于中国，投资效率（每单位投资增长的GDP增量）呈下降的趋势。<br />
<br />
6、货币供应的强劲增长造成的低利率也会引发失衡和泡沫。自17世纪30年代得郁金香狂热事件以来，在所有的重大投机背后，总有宽松货币的身影。低利率让投资者寻求更加高回报，更有风险的投资。Walter Bagehot写道，“英国佬（John Bull）可以承受许多事情，但却承受不住2%的利率。”他评论道，当利率下降到如此微薄的水平，就驱使人们用千辛万苦攒下来的积蓄去投资一些不靠谱的东西－－比如一条通往堪察加的运河、一条去沃切特的铁路、一个让死海复活的计划、以及一个往热带运输冰刀的企业。<br />
<br />
7、外汇储备－－许多评论家看到中国高达2.4万亿的庞大外汇储备，感到很安全。然而，这么大量的美元应该是令人担忧的。它反映了国际货币体系的深刻不平衡。有人认为，中国的巨大外汇储备能使中国经济立于不败之地，这其实不对。这些储备可以用来购买外国资产、进口、或防御货币攻击。但有一类问题，它解决不了，比如，在资产泡沫破灭后，银行坏账大量出现，不良投资到处都是。正如一位评论家指出，相对于全球GDP，只有两个国家先前曾经累积过如此庞大的外汇储备，他们是1929年的美国和1989年的日本。<br />
<br />
8、股市泡沫－－信贷激增重燃了中国投资者的动物精神。去年7月下旬的某一天，上海A股的成交量超过了纽约、伦敦和东京三大证券交易所的成交量之和。去年第三季，以市值论，中国IPO占了全球的三分之二。全球最大的10个IPO，中国占有七个。新股往往高倍超额认购，上市首日暴涨。去年10月，深圳创业板市场推出。开市首日，28家创业板上市公司涨幅在76％至210％之间，市盈率平均达150倍。高换手率、新股大量发行、新股一上市即劲涨、成立新交易所，这些都投机狂热的经典迹象。去年夏天，市场见顶时，上证指数的PE倍数为38倍。<br />
<br />
9、房地产泡沫－－不过，股市只是一个插曲，真正的大戏发生在中国狂热的房地产市场。文中讨论的许多红色警报信号在中国狂热的房地产市场随处可见：有一个夺目的增长故事（正在进行的城市化和未来的经济高增长率）；建设热潮帮助中国实现 GDP增长目标；低成本信贷流入房地产领域；腐败到处都是；道德风险普遍存在，相信政府不会让楼市崩溃；资产价格高估，投机盛行，凯恩斯讲的“动物精神” 到处都是。<br />
<br />
关于钱塞勒(Edward Chancellor)<br />
<br />
钱塞勒是金融方面的历史学家和金融泡沫专家。<br />
<br />
他1999年出版其投机史经典之作－－《贪婪时代－－金融投机的历史》，这部史书研究了过去300年来的投机狂热。记述了由1630年的荷兰的郁金香狂热，到英国铁路股票热潮，以至日本泡沫经济爆破的数个投机热潮，当然也少不了1929年10月24日，触发全球大衰退的华尔街股市崩盘，20世纪90年代高科技泡沫，当时格林斯潘声称预测任何泡沫都是徒劳的，但是该书准确预言了高科技泡沫的破灭。<br />
<br />
钱塞勒在其2005年2月出版的《信贷巅峰时刻》一书中预言，美国和英国的房地产市场“看上去非常脆弱”，可能因利率上调而崩溃，届时借款方会遭受损失，大量不良贷款使得贷款方的资产负债表不断恶化，终将引发信贷危机，某些情况下，还可能引发全面的银行危机。两年之后的2007年，美国房地产次级贷款危机爆发，不幸被钱塞勒言中。<br />
<br />
而这一次，他将注意力投向中国，通过与过去300年来的著名泡沫的特征相比较，揭示当前中国经济增长所呈现出的泡沫症状。</div>

							
							
							
							
