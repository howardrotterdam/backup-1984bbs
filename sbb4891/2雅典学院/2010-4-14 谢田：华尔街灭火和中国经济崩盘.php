													<h2>谢田：华尔街灭火和中国经济崩盘</h2>
						
						
																											<div id="postmessage_562496" class="t_msgfont">西方专家评判中国经济，有合理分析的依据，但缺乏对中共本质的理解。中国经济崩盘不只是经济层面的事，它一定伴随着更广泛、更深刻的社会变革。<br />
<br />
&nbsp; &nbsp;真相离世人越来越近，世界对中国越来越不信，这是中共当局最为恐惧的。北京对外界批评有个指责，颇具中国特色，说外人对中国“说三道四”、“指手划脚”。对“说三道四” 耿耿于怀，终于有反唇相讥的机会了。日前中国组团几位经济学家赴美，给华尔街银行家“上课”，反映了跨太平洋微妙的互动。<br />
<br />
&nbsp; &nbsp;　　人多力量大的专家团<br />
<br />
&nbsp; &nbsp;“人多力量大” 虽然过时，影响尚在，人海战术居然用到学术和商业界了。其实，中国经济学家给华尔街上课，如果说的有道理、有说服力，来一个专家就成；甚至半个专家也够了。来多了也不是不行，来两个意见相同，也说得过去；三、四个持统一观点，就难以理解。一下子来六个，并且六人的观点一致，就未免令人生疑。<br />
<br />
<br />
&nbsp; &nbsp;学术界中，经济学家各执一词、意见相左，最闻名遐迩。中国经济错综服杂，六个专家异口同声，同唱一首歌，合着中南海的调子，怪不得美方在上完“课” 之后，还是半信半疑。<br />
<br />
&nbsp; &nbsp;其实，坦称的美国佬已告诉客人，中国学者如能坦率一些、真正指出问题所在，他们反而会 “更放心一点”。中国学者异口同声，在遮盖什么呢？何以恰恰在“中国经济崩溃” 的预测纷纷出台时，急于华尔街灭火？<br />
<br />
&nbsp; &nbsp;崩溃的种种说法<br />
<br />
&nbsp; &nbsp;《新闻周刊》预测今年十大事件，第二件就是中国经济崩溃。《新闻周刊》预测中国股市和房市产生泡沫，最后导致崩溃。《福布斯》去年12月也指出，中国经济目前的状况，和20年前日本股市和房地产市场崩溃的前夕没什么两样，两者都以借贷和哄抬房地产价格引发的资产泡沫为特征。<br />
<br />
&nbsp; &nbsp;西北大学中国问题专家维克托・施(Victor Shih) 更是直截了当，说中国经济实际上是个“庞氏骗局”，骗局由央行主导，一面印钞票安抚，一面大肆敛财。<br />
<br />
&nbsp; &nbsp;成功预测安隆 ( Enron ) 倒闭的查诺斯(James S. Chanos) 也说，中国去年的刺激政策导致产出供过于求，银行坏帐风险升高，因而将导致中国经济的崩溃。自信的查诺斯还放空中国水泥、煤炭、钢铁等相关公司的股票，希望从崩溃中获利。如果查诺斯悄悄放空，也许真能赚一笔。但他放空后还很高调，让中共丢了丑。熟悉中共的人，恐怕就不会押他的注了。<br />
<br />
&nbsp; &nbsp;崩盘判断的偏颇<br />
<br />
&nbsp; &nbsp;西方判定中国经济崩盘，有理性的分析，但缺乏对中共本质的认识。查诺斯从去年夏天开始研究中国经济，这倒不是问题，后来居上也有。但他把中国经济和政治分开了，所以他的结论按正常社会的逻辑去推，可能非常正确，但用在中国市场，就会出偏。<br />
<br />
&nbsp; &nbsp;查诺斯描述的“中国超额信贷规模之大，全球无出其右” ，的确是中国经济出现麻烦的根源之一。但对付信贷扩张，红朝运作自如。君不见几年前那么多银行坏账都被悄悄转移，从“优良资产” 中剥离，并成功推销出去了吗？这次超额信贷的坏账，规模当然更大，但中共重施故技，处理它应该不是问题。<br />
<br />
&nbsp; &nbsp;判断偏颇的原因，是不了解中共治下的运作，以为它跟正常社会一样，货币发行过多，就会通货膨胀；商品供给过多，价格就会下降；银根略有放松，贷款必然增加等等。中国经济如果按西方的模式去推，肯定是会崩盘的。<br />
<br />
&nbsp; &nbsp;但哪天西方也许会惊奇的看到，中共又一次从崩溃中“缓”了过来，西方经济理论又一次“失灵” 。这是因为，只要中共存在一天，它就会拚死命去保持这个畸形的经济不会“崩溃”，保证这个已经烂透了的苹果衣着光鲜；中共会倾尽全部国力，继续营造繁荣的假象，直到假象和骗局最后被完全揭开。<br />
<br />
&nbsp; &nbsp;国寨也会装模作样的运用一些西方经济学的杠杆如“利率” 、“银根” 、“准备金” 等来调节。但这些自由资本主义的工具，在极权面前什么也不是。假杠杆的运用，更有欺骗性，中南海一句话，就会令其变成一纸空文。经济问题一旦涉及中共存亡这个根本，就会被弃之不理。<br />
<br />
&nbsp; &nbsp;究竟怎样崩盘<br />
<br />
&nbsp; &nbsp;既然如此，中国经济会因此而永不崩溃？当然不是。笔者要指出的是，中国经济不会像一般人想像的那样，按经济规律崩溃。而且，中国经济的崩盘，不只是经济层面的事，它一定是伴随着更大、更深刻的社会变革而来的。<br />
<br />
&nbsp; &nbsp;在中国经济一天天被权贵阶层、太子党集团、依附权贵的知识界附庸等日益掏空、反覆榨取之下，崩溃是必然的。但因为“保几” 之类政策背后的努力，它会以一种出人意料、令世人瞠目结舌的方式展现，而不会以正常社会的经济萧条、衰退等方式体现。<br />
<br />
&nbsp; &nbsp;西方专家的预测，一定是会落空的。落空的原因很简单，他们低估了中共为自己的利益，为自己的生存，会不惜鱼死网破、破釜沉舟，把全中国社会、人民、和经济，都一起拉下水、拉入万劫不复地步的“坚定决心” 和“坚强意志”。<br />
<br />
&nbsp; &nbsp;当然，有些西方人还是明白的，或至少在明白之中。法国人最近就说到了点子上。法国时事评论杂志玛丽安发表评论文章，作者是政治学者瓦莱丽・尼凯，文章题目是《中国的最大敌人就是中共》，认识到了中共是中国的绊脚石。<br />
<br />
&nbsp; &nbsp;一位网友留言说，“''中国经济如果不崩溃的话，就没有天理了。''” 这话说对了，因为违背了天理，国人才有如此深重的苦难；而天理不昭，问题就不会解决。天理昭昭之日，崩溃的就不只是中国经济，导致崩溃的源头－中共本身，会率先崩溃。<br />
<br />
&nbsp; &nbsp;耶鲁金融教授罗伯特・席勒是房地产席勒指数的发明者。他说中国的房地产市场缺乏一个像美国大萧条的“坏故事”。无论是普通购房人、投机者、房地产商和各级政府，都沉迷于好故事的情节里。“中国需要坏故事的警醒，否则非理性繁荣持续越久，其破坏力就会越大。” 诚哉斯言。</div>

							
							
							
							
