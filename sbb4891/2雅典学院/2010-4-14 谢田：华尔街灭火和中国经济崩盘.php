													<h2>长期操纵货币将导致中国经济崩溃</h2>
						
						
																											<div id="postmessage_562500" class="t_msgfont">在 &lt;人民币被低估了40％吗？&gt;一文我们谈到人民币兑其它货币(加币等)近年来贬值了40%左右，并且中国政府操纵货币(currency manipulation)上瘾。同时，人民币被严重低估令中国老百姓深受其害。我们再来探讨一下关于人民币的其它问题。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; &lt;人民币近年的走势&gt; <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 简单地说，人民币在2003年和2004年间，贬值了40%左右，加上人民币本应有的升值，贬值大约在50%左右。<br />
<br />
&nbsp; &nbsp; 随后，由于国际社会的压力，在2006年和2007年人民币大约升值了20%左右。<br />
&nbsp; &nbsp; 而从2008年底到目前，人民币又再次贬值了10%左右。总体来说，从2003年到目前，人民币目前大约被人为贬值40%。(和整体货币比较，而不是仅仅和美金比较。)<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; &lt;中国经济短时间内不会崩溃&gt;<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 很多人在讨论中国经济会不会崩溃？我个人认为长期来看，中国经济必然崩溃(德国纳粹政府，前苏联都有过类似的辉煌)，这是违反经济规律的结果。但短时间内，中国经济绝对不会轻易崩溃，认为中国经济短时间内会崩溃，是一种误判。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 主要原因是中国政府掌握着汇率＂筹码＂。大家想一想，在2003年至2004年，中国经济高速发展的时候，中国政府还＂厚着脸皮＂让人民币一次性贬值40%。那么一旦中国经济有崩溃的迹象，中国政府可以让人民币再一次性贬值50%。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 中国政府让货币贬值是不需要代价的，只要一句话就可以。如果中国政府愿意，明天就能让汇率降低50%。而不象其它国家，如果想减低汇率，只能间接干涉，例如，发行货币购买国债，调整利率。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 中国政府操纵货币已经上瘾，而这种上瘾的做法也确实可以让中国的崩溃推迟。一旦中国经济势头不好，中国政府可以人为操纵货币大贬值来拯救经济。一旦中国经济势头良好，中国政府可以以维持汇率稳定为借口，保持货币被严重低估。中国政府在操纵货币的同时，却希望国际社会承认中国是市场经济，实在是可笑之极。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 所以说，我们不用担心中国经济短时间内崩溃，中国政府从操纵汇率的第一天起，就有心理准备一直操纵下去。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; &lt;通货膨胀会把老百姓推向绝路&gt;<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 中国人忍耐力特别强。中国人不是忍无可忍的情况下，只有有一口饭吃，都是不愿意和政府做对的。官员再贪污腐败，再官商勾结，只要还有饭吃，就忍了。所以说，近年来尽管通货膨胀十分严重，老百姓还是忍了。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 可以断言，如果人民币继续严重低估，中国的通货膨胀会更加严重。严重到什么程度？直到中国所有的物价都赶超欧美。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 别忘了，中国的民工的收入只有欧美工人的20分之一，如果承担着欧美的物价水平，还能活吗？这也是中国沿海开始＂民工荒＂的原因。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 中国目前的房子，汽车，汽油，服装，家具，电器，电脑等等已经和欧美一个物价水平了。在几年内，日用品（牙膏等），食品（粮食，蔬菜）也会达到欧美的物价水平。可以肯定的是，中国的通货膨胀必然会继续下去，两年之后，中国老百姓对高物价低收入还会有耐心吗？<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 中国老百姓在经历了＂工资不涨，物价天天涨＂之后，在经历了＂经济高速发展，医疗教育养老却要自费＂之后，在经历了＂少数人富起来了，大多数人却更贫穷＂之后，在经历了＂国有资产都被私有了，我们却什么也没捞着＂等等之后，还有多少耐心等待中国政府让老百姓富裕起来？中国老百姓终于会明白，中国老百姓如果希望象日本韩国那样，每个国民都富裕起来，（在一党专制的政府的领导下）是不可能实现的？所以，中国政府的政治崩溃很有可能在经济崩溃之前。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; &lt;奥巴马政府是不负责任的政府吗？&gt;<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 最近有个中国社科院的专家说：克林顿政府是负责任的政府，让美元强硬，奥巴马政府是不负责任的政府，让美元贬值。<br />
