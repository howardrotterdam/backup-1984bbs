<br />
我欢迎谷歌图书馆扫描我的每一本图书，并欣然接受在显示目录和摘要下60美元一本的条件。之前对于谷歌的指责是因为我在接受采访时受到了某些媒体的误导，而当时在外地比赛并无查证的时间和条件，特此收回。<br />
<br />
经过对比，我发现这是非常合适的一个条件，谷歌只显示我的书的目录和摘要，就支付给我60美元。回首祖国，无数的网站都能下载我的书的全文，从1999年 2009年，我从来没有收到过一分钱。现在谷歌仅仅刊登了我的书的目录，就支付给我60美金，我觉得非常的满足。这十年来，我曾一度接近可以通过互联网赚点稿费，在2000年的时候，某中文网曾经和我签约第一本书的电子版权，在点击和下载量终于可以接近10000人民币的时候，突然间所有数据都变成0了，后来就再也没有人提起过这事。在中国，唯独起点中文网支付给我网络连载的费用，但那是针对尚未出版的新小说。<br />
<br />
而谷歌支付的这60美元仅仅是个目录而已，如果有人要全文下载，那需要另外收费，如果我拿七谷歌拿三，我认为是非常合理的，因为在传统出版中，我只拿一，书店要拿到四到五。<br />
<br />
在谷歌数字图书馆之前，作为内行，我甚至都不知道中国还有文字著作协会。我一直以为在中国负责版权保护的人都已经死绝了。可能打击国内的盗版网站工作量大而且没有什么钱可以赚吧，所以他们一直潜伏着没有出动。也可能对中国的所谓版权保护协会来说，有奶才是敌。既然有这个协会，那我恳请他们帮我维权，打击除了谷歌以外一切可以免费阅读下载我的长篇小说全文的文学网站（我愿意将我的杂文，散文，短篇小说在网络上免费共享，但长篇小说和整本图书实在不行，希望大家留我一条生路，虽然如此，如果你要强行共享，也完全可以搜索到我的任何文字）。<br />
<br />
谷歌是第一个愿意就已出版图书支付给我钱的网站，出于对先烈的优惠，我给他们打五折，也就是30美元一本，即可获得我的授权。<br />
<br />
