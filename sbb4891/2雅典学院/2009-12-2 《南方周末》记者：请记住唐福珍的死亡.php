写完上海女人潘蓉抗击拆迁的稿子，已经是晚上11点28分。记者应该秉持客观中立的态度，不能动情，然而，写这篇稿子的几个小时里，我一直在流泪。<br />
<br />
&nbsp; &nbsp;我的好友浦志强一再告诫我，不要把自己混同于采访对象，要克制情绪。我知道他说的是对的，但是今天，我做不到。因为唐福珍死了。这一次，我并没有把自己混同为采访对象，但我不不仅仅是记者，我还是人。<br />
<br />
&nbsp; &nbsp;很多年前，在北京日报工作的时候，幼稚的我渴望新闻自由，写了一篇小说《真相》。小说中的女记者无意中遇到了一位农民工，讨薪不成，妻子被医院骗了高额医疗费却落下了残疾，两个孩子被人贩子绑架后，警察索要办案费。受体制和新闻规律所限，她无法报道此事，农民工用跳楼自杀来成全了她的新闻价值诉求。最终，女记者说服报社总编辑，报道了这条新闻，农民工的问题得到了解决，然而，两人都遭到了停职处理。<br />
<br />
&nbsp; &nbsp;这个幼稚的小说躺在我的电脑里，至少有3年的历史。我没有想到，现实生活的故事远比这个小说激烈。据我的一个媒体朋友透露，唐福珍生前多方求助于媒体，有的媒体勒索她，更多的媒体是袖手旁观，因为案子不够大，不够极端。是啊，在到处都是拆迁惨祸的中国，有几起能够进入媒体视野的呢？潘蓉用燃烧瓶扔挖掘机极端吗？一个村选出几名代表经年累月上访极端吗？手拿宪法、物权法保卫财产极端吗？在市区中央搭窝棚一住几年极端吗？都不极端。唐福珍想用她的鲜血，刺激这个社会的良知。自焚前她说：“一定要选一张我最漂亮的照片做遗像。”她的极端，确实引来的媒体的关注，甚至她的死亡，比自焚后救活更能带来新闻效应。<br />
<br />
&nbsp; &nbsp;然而，她用对自己的残酷换来的些许期望遭到了践踏。很可能，关于她的讯息，已经无法在报纸、电视上出现，也许还有网络。当然网络是删不干净的。删不干净的网络却只能徒增我们的痛苦。我们不能上街，不能散步，这些法治国家允许做的公开表达愤怒的行为，我们都不能做。<br />
<br />
&nbsp; &nbsp;除了被拆迁者本身，也许我们记者是最能体会他们痛苦的一群。因为我们看到过、听到过、拒绝过、愧疚过、麻木过、袖手旁观过。我很想向九泉之下的唐福珍道歉，但是我知道，我没有资格。因为如果她还健康地活着，我和同行们一样，会拒绝她的请求，我无法将她的故事写在报纸上，我依然会眼睁睁地看着这个和我一样爱美的女人拼死一搏后，撒手人寰。迄今为止，除了失声痛哭，我别无他法。“我们都是体制”，连岳曾说。唐福珍的死，是我们所有人都无法洗清的罪过。<br />
<br />
&nbsp; &nbsp; 这是个该诅咒的世界。请记住唐福珍的死亡！<br />
