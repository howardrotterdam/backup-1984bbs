<br />
&nbsp; &nbsp; <br />
1.要求上海市浦东新区城市管理行政执法局就原南汇区交通行政执法大队栽赃陷害执法一事在上海媒体,河南媒体,及中央媒体向孙中界先生公开承认错误,赔礼道歉,恢复孙中界先生名誉.<br />
