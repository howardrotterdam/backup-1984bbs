													<h2>郝劲松:钓鱼执法 水落石出</h2>
						
						
																											<div id="postmessage_300315" class="t_msgfont">10月25日下午7点,.郝劲松得到可靠消息,&quot;上海10.14孙中界断指事件&quot;取得突破性进展,整个事件由原南汇区交通行政执法大队与&quot;钓钩人员&quot;相互勾结,栽赃陷害孙中界并扣留车辆,10月26日,上海市组成的调查组将向社会正式公布详细调查结果.<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;郝劲松认为:孙中界被栽赃陷害仅仅揭露了冰山一角,据保守估计在上海每年被&quot;钓鱼式执法&quot;所栽赃陷害的车辆有数千辆.自上海钓鱼执法被媒体揭露后,郝劲松每天都收到全国各地大量车主的来信和电话,反映自己被钓鱼执法的情况,目前.郝劲松及助手正在做这方面的统计和调查工作.<br />
<br />
<br />
&nbsp; &nbsp; 郝劲松说:在上海,黑幕重重的钓鱼执法已形成一个庞大的产业链,巨额的罚款金额撑起了一个畸形的执法平台,一些执法人员和社会不良人士在暴利的刺激下,丧尽天良,用栽赃执法的方式陷害合法公民于非法的处境,并非法掠夺公民的合法财产,在执法过程中,使用暴力对车主进行人身攻击,非法拘禁,野蛮搜身,严重践踏侵犯公民的宪法权利.上海市政府应当组成专案组对&quot;闵行区张军钓鱼执法案&quot;也进行调查,并对近年来上海市18个区县的查获非法运营车辆的所有案件重新核查,同时向社会发布公告,让曾经被钓鱼执法的受害车主向调查组报名登记,重点核查.<br />
<br />
<br />
&nbsp; &nbsp;郝劲松说:根据刑法第266条,诈骗罪是指以非法占有为目的，用虚构事实或者隐瞒真相的方法，骗取数额较大的公私财物的行为。、1.犯本罪的，处三年以下有期徒刑、拘役或者管制，并处或者单处罚金；<br />
　　2、数额巨大或者有其他严重情节的，处三年以上十年以下有期徒刑，并处罚金；<br />
　　3、数额特别巨大或者有其他特别严重情节的，处十年以上有期徒刑或者无期徒刑，并处罚金或者没收财产。<br />
　　根据《最高人民法院关于审理诈骗案件具体应用法律的若干问题的解释》（1996年12月16日）的规定：个人诈骗公私财物2千元以上的，属于“数额较大”；个人诈骗公私财物3万元以上的，属于“数额巨大”。个人诈骗公私财物20万元以上的，属于诈骗数额特别巨大。<br />
　　<br />
<br />
&nbsp; &nbsp; 郝劲松认为:数量众多的&quot;钓钩人员'与上海各区交通执法大队的成员相互勾结,狼狈为奸,用虚构事实,隐瞒真相的方法栽赃陷害大量车主，骗取公私财物,数额特别巨大,情节特别严重,已涉嫌有组织犯罪,上海市警方应当在18个区县立即成立&quot;钓鱼执法&quot;专案组,迅速立案,对众多&quot;钓钩&quot;,&quot;钓头及背后唆使的交通执法队员&quot;实施布控抓捕,及时破获这个严重危害社会秩序,造成社会严重不稳定因素的有组织犯罪团伙.<br />
<br />
郝劲松说:现有的打击黑车手段,治标不治本,一方面在城乡结合部存在大量出租车市场需求空缺,另一方面,有关部门却不允许个体户从事正规出租车运营,市场需求导致个体私车成为黑车运营.要想真正消除黑车,只有对现存的出租车制度进行改革,降低出租车准如制度的门槛,打破现有的出租车垄断市场,允许个体户从事出租车运营,运用市场调节的杠杆,可从根本上消除黑车.<br />
<br />
