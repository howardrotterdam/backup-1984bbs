<br />
 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;主张在中国大搞300年殖民地的一个汉奸文人获诺奖了。<br />
<br />
&nbsp; &nbsp; 大大小小的汉奸、有汉奸倾向的人们裹挟部分好事之徒，在这个周末弹冠相庆，扯上横幅表示祝贺，有人喜极而泣，仿佛已经胜利到来了。<br />
