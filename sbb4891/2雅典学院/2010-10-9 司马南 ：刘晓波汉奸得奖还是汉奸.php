<br />
&nbsp; &nbsp;司马南/文<br />
<br />
 <br />
<br />
&nbsp; &nbsp; 主张在中国大搞300年殖民地的一个汉奸文人获诺奖了。<br />
<br />
&nbsp; &nbsp; 大大小小的汉奸、有汉奸倾向的人们裹挟部分好事之徒，在这个周末弹冠相庆，扯上横幅表示祝贺，有人喜极而泣，仿佛已经胜利到来了。<br />
<br />
&nbsp; &nbsp; 评奖委员会给这个汉奸文人的一个隐秘版本的颁奖词如下：<br />
<br />
&nbsp; &nbsp;This award, given vision and perseverance, perseverance, and the birth of the world in 1956, and Red China is destined to bring trouble to give the Chinese people. 20 years ago, you sent the strongest voice of the free world, &quot;China should engage in 300 colonies,&quot; you did not hesitate to sell the interests of their country of birth, perseverance and original material to attack the communist regime, no matter how much China has made Great progress, you can always successfully braved the legal risk, find or humiliate them automatically generated material. You are a good crusader, is also a practitioner of universal value. Today, China has acquired the status of the world's second economic output, you work for the interests of the yuan revaluation, and according to the U.S. political system is very important.<br />
（译文大意附在文末）<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; 1，这事早有端倪<br />
<br />
&nbsp; &nbsp; 这是一个早就看出端倪的事情。前不久挪威方面放出话来，声称受到中国政府压力，中国外交部傅莹副部长表示如果授奖给那个主张中国应该有300年殖民地的哥们，将严重损害中国与挪威的关系，但是他们挪威国小牛逼大，诺贝尔研究所的所长盖尔・龙德斯塔德表示，诺贝尔委员会将不会屈服于外界压力。<br />
<br />
&nbsp; &nbsp; 几年来，由哈维尔等人出面，立志与中国为敌的一批气味相投的势力一直在抱团儿为此结果而努力，那个全世界的事情没有他不管的大国给挪威施加的压力其实更大，只是挪威方面不敢说不便说罢了。<br />
<br />
 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; 2，诺奖 K线图清晰<br />
<br />
&nbsp; &nbsp; 瓦文萨点燃了东欧剧变的导火索，瓦文萨首获诺奖；最早出来以不同政见者身份策应颜色革命的萨哈罗夫劳苦功高，萨哈罗夫即获诺奖；戈尔巴乔夫内部爆破弄垮了苏联，戈尔巴乔夫终获诺奖；达赖喇嘛分裂中国不遗余力，达赖喇嘛也获诺奖；高行健《一个人的圣经》用文学语言精巧地妖魔化中国，高行健获诺奖；富一代热比娅新疆策动分裂闹事，热比娅获诺奖提名；高大婶偏执型人格有精神障碍根本不靠谱，但她夸张地介绍中国艾滋病，富有感染力的数字可以随着情绪忽高忽低，高大婶也获诺奖提名；一退休个老军医接受外国记者采访，声称中国的萨斯死亡人数不可计数，退休老军医也成为诺奖候选人……诺奖K线图清晰地告诉我们，谁能让目标国的人最大程度地蒙羞，谁心理素质好到可以接近不要脸地卖国求荣，谁差不多就有机会问鼎诺奖了。<br />
<br />
 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; 3，诺奖的耻皿价值<br />
<br />
&nbsp; &nbsp; 不能说诺奖没有价值，一帮人每年费那么大心思搞出来，总得有点意义，这个意义明面上讲是推进人类和平事业，但是这丝毫不妨碍颁奖委员会吹黑哨踢黑球，直接把和平奖颁给战争贩子。于是，就像球场上的球迷们迷惑不解地看着“足球异动”一样，全世界人民惊讶地看到那个诺贝尔和平奖的获奖者一边往阿富汗调兵遣将，一边往台湾出售63．92亿美元的进攻性武器，一边拿着诺奖侃侃而谈的无耻画面。<br />
<br />
&nbsp; &nbsp; 硕大的无耻，有获奖者的一半，也有颁奖者的一半。<br />
<br />
 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; 4，羞辱中国的结果<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;有人关心，汉奸获奖的结果能把中国怎么样？<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;我看不出能把中国怎么样。当年实力对比极其悬殊的时候，在朝鲜三八线与越南丛林里交手，老大也没完胜，况乎今天？<br />
<br />
&nbsp; &nbsp; 但是，这话成立有个前提，前提是我们不可自侮之自毁之。比如，当有人以“诺奖是普世价值体现，中国政府应该云云”的时候，我们的接访者必须庄严笃定义正词严，不能动不动就哭一鼻子，交一会子心，与人坦言普世价值是什么人类共同追求。再比如，当美国总统奥巴马，也是另一位诺奖得主，呼吁释放某某某的时候，你必须有勇气正告之：不，中国有独立司法权，这一段不归你管，而不能骨酥腿软，任由其摆布。<br />
<br />
&nbsp; &nbsp; 人家打了你一巴掌，羞辱了你的祖国，戕害了人民的尊严，你不能反而去当面瓜，说好话，当好学生，争取好印象，应该有一那么一点骨气，最好在太上老君八卦炉里炼上七七四十九天，至少练好了八段锦易筋经，验能百毒不侵，坚定中国的文化自信与体制自信之后再出去与人对话。<br />
<br />
 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;5，诺奖迷信该撤火了<br />
<br />
&nbsp; &nbsp; 直到今天仍然有人热衷于讨论，中国人何时能得诺奖？中国人得不到诺奖说明了什么？在我们某些人的眼睛里，在持续30年之久的鹦鹉学舌般的宣传中，诺奖完全是正面的、神圣的、高不可攀的，这种世界上鲜见的“逆向种族主义”言论氛围导致了诺奖迷信在中国日甚一日。那几个科学奖的真面目我不清楚（留待方舟子与肖传国等科学界人士解释吧）但是，冷眼观看什么文学奖、和平奖的走势，一次又一次重复，一圈又一圈循环，傻子都看得出来，明摆着与中国人民为敌。<br />
<br />
&nbsp; &nbsp; 从20年前，国内就应当停止这种神话诺奖自我戕害的病态宣传，但是，很遗憾，没有。因为做不到，因为我们很多人已经忘记当年的改革开放是什么意思，因为今天大胆的狂徒高喊思想解放没有禁区，已经离开了所有的原则底线，“四项基本原则”业已成为老古董被带头大哥抛在脑后。<br />
<br />
 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; 6，汉奸不是“异见人士”<br />
<br />
　&nbsp;&nbsp;我外交部发言人马朝旭８日表示，诺贝尔委员会把今年的诺贝尔和平奖授予某某某，完全违背该奖项的宗旨，也是对和平奖的亵渎。<br />
<br />
&nbsp; &nbsp; 话说得不错，表态也及时。但是，马朝旭似应该对这个提问者的话本身做一点拆解：你说诺贝尔委员会８日把今年的诺贝尔和平奖授予中国“异见人士”某某某。第一，这位某某某不是什么“异见人士”，他没有什么“异见”，与海内外那些敌视中国、反对中国的人一个鼻孔出气，叫什么异见？按中国的老词，过去这叫“汉奸”，“买办文人”。汉奸就是汉奸，没必要起什么新的名字。第二，此人不仅在言论上反对中国，而且身体力行十分买块儿，他的具体行为触犯了包括中华人民共和国宪法在内的法律原则。此刻，这个人正在中国的监狱中服刑，而中国正在致力于建设法治国家，我们敬爱的总理温家宝常讲“法大于天”，难道你们的诺奖要颠覆比天还大的法律不成？<br />
<br />
 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; 7，诺奖的钱从哪来的？<br />
<br />
&nbsp; &nbsp; 一个故事讲了100多年，人们信了100多年――造炸药的诺贝尔先生良心发现，把自己的遗产捐出去设了无关政治，不讲文化，没有国界，甚至没有价值倾向的诺贝尔奖金。但其实，那不过是长篇小说“诺贝尔故事”的引子而已，当今的诺奖的主体是是欧美共济会的金融基金───主要是罗斯切而得金融家族。敬请参见何新最新文章，《揭秘：诺贝尔奖基金会的真正金主其实也是共济会》，这里我不赘言。据此，大家不难理解，为什么诺奖背离了创办者的原意，失去了公正性，沦落为操纵者的工具。质而言之，出资者决定游戏规则。<br />
<br />
 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;8，没获奖的汉奸更危险<br />
<br />
&nbsp; &nbsp; 前面说了，获奖的汉奸也是汉奸。还有一句话，必须同时讲出来，没获奖的汉奸也是汉奸，他们更危险。获奖的汉奸在押，关着呢。没获奖的汉奸在外边兴风作浪，在媒体上常常以什么“公公知识分子”的名义出现，到处讲什么普世价值，讲什么“不要现代化，要现代性”，因而颇具迷惑性。<br />
<br />
&nbsp; &nbsp; 譬如那位遭到《南方周末》热捧的“公公知识分子”徐友渔，他为推动此番汉奸获奖使出吃奶的劲，做出了重要努力。他专门给诺奖评审委员会发信,强调某某某如果获奖，将传递“埋葬专制统治”、“埋葬暴力”的价值。徐友渔笔下的“专制统治”指的是谁非常很明确，而“他要埋葬的暴力”，绝不是是指在伊拉克、阿富汗、南联盟制造血肉横飞大片效应的真实施暴者。<br />
<br />
&nbsp; &nbsp; 汉奸们是爱憎分明的，他们堂而皇之地拉着鬼子进村，挨门挨户地翻箱倒柜……大嗓门的、敢下手的、六亲不认的铁杆汉奸都憋着要通过表现而获奖，远远望去，他们的背上都绣有一个大大的“诺”字。<br />
