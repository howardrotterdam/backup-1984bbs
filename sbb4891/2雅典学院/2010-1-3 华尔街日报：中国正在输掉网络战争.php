													<h2>中央将收紧网络管理</h2>
						
						
																											<div id="postmessage_414855" class="t_msgfont">史上首次用电视电话会议形式召开全国政法工作会议，不同寻常的是各省市县党政一把手都参加了会议<br />
<br />
　　日前，2009年度全国政法工作电视电话会议在北京京西宾馆召开。此次会议一改过去的惯常做法，利用3个小时的时间，采取电视电话会议形式召开全国政法工作会议，这在历史上是第一次。同时，中央政法各单位不再分系统另行召开年度工作会议。特别值得注意的是，各省、市、县党委和政府主要负责同志都参加了此次电视电话会议。<br />
<br />
<br />
　　近年群体性事件接连发生<br />
<br />
　　会议指出，面对形势的深刻变化，政法工作还存在诸多不适应。因此，下一阶段，全国政法机关将紧紧抓住影响社会和谐稳定的源头性、根本性、基础性问题，深入推进社会矛盾化解、社会管理创新、公正廉洁执法三项重点工作。<br />
<br />
　　根据中央要求，化解社会矛盾，关键在于抓源头，清积案，建机制，强基层，努力化解老矛盾，有效预防新矛盾，进一步形成依法有序表达诉求、及时有效解决社会问题的社会环境。<br />
<br />
　　从近年来高位运行的信访总量、接连发生的群体性事件中不难看出，这些信访和群体性事件背后反映的大多是因利益诉求而引起的人民内部矛盾，是改革发展过程中的问题。<br />
<br />
　　有关专家指出， 群体性事件、个人极端事件是矛盾问题堆积、激化的结果。中央已经明确要求，各级政法机关要在近年工作的基础上，进一步集中力量，打一场排查化解矛盾的攻坚战，努力化解老矛盾，有效预防新矛盾，更加注重机制建设。<br />
<br />
　　<br />
<br />
　　一些新媒体带来负面作用<br />
<br />
　　进入新的发展阶段，我国社会事业建设与经济发展不协调的问题日益凸显，特别是社会管理仍存不少薄弱环节。反映到高层的信息显示，近年来一些城中村、城乡接合部成为治安乱点，一些特殊人群成为违法犯罪的主体，一些新媒体为有害信息的传播和不良舆论导向提供了平台。所有这些，使加强社会管理工作面临着前所未有的严峻挑战。<br />
<br />
　　从中央要求看，能不能尽快改变这种状况，不仅是对政法机关执行能力的考验，也是对我们党执政能力的考验。<br />
<br />
　　针对社会管理的薄弱环节，中央明确要求，应下大力气尽快解决一些重点问题。<br />
<br />
　　<br />
<br />
　　QQ群和微博都要加强监控<br />
<br />
　　有关权威专家解读说，一要解决好流动人口服务管理问题。二要解决好特殊人群帮教管理问题。三要解决好社会治安重点地区综合治理问题。四要解决好网络虚拟社会建设管理问题。互联网不仅是新技术、新媒体，也是新的意识形态阵地。深入推进社会管理创新，需要重点解决好网络虚拟社会建设管理问题。按照中央要求，下一阶段，应坚持建设与管理并重，法律手段、行政手段、经济手段、技术手段并用，明确电信运营企业、用户的法律责任，最大限度地发挥网络积极作用，最大限度地遏制网络消极影响。五要解决好社会组织管理服务问题。<br />
<br />
　　据悉，根据中央要求，法院、检察院、公安、司法各单位也已展开具体部署，落实中央要求。<br />
<br />
　　在参与社会管理方面，政法各单位也部署了一系列措施。例如，公安部提出，将有效整合各种资源，加强网上管控，将网警力量向县级公安机关延伸，将网上巡控触角向QQ群、微博客等管理薄弱空间延伸，提高网上发现、侦查、控制和处置能力，严防形成隐蔽性犯罪组织。（《t望》）<br />
