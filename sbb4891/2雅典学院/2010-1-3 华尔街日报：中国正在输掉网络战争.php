													<h2>中共正在输掉网络战争</h2>
						
						
																											<div id="postmessage_414055" class="t_msgfont">中共所谓的反网络色情运动已经进行四个月了，中共政府关闭了数千个网站，其中有些是色情的，很多却不是。尽管中共不断的对网络实施更严密的封锁，有数不清的网站在中国被扼杀，然而更大层面的事实是：网络审查正彻底失败。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;《华尔街日报》记者ORETTA CHAO和JASON DEAN十二月三十一日报导，外国网站，比如Facebook, YouTube 和 Twitter，自从中共准备十月一日庆祝其在中国的六十年统治那天起就遭审查封闭，至今许多中国用户依然无法连上。 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 自从网络进入中国后的十几年来，曾遭受中共多次打压，被批评者们称之为“超级防火墙（Great Firewall）”的网络过滤装置，其封锁的范围不断扩大，封锁技术也越来越尖端。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 艾撒克・毛（Isaac Mao）是创办中国博客的先锋和中国网络的研究者，他说：“尽管审查在各处战役中都有获胜，却在输掉整个战争”。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 在二零零九年，北京甚至在一场大战役中失败，也就是所谓的“绿坝”事件。这是网络审查力量有限的最戏剧性的表达。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 现在“翻墙”这一网络管制异议者的术语，已经成为许多中国网络用户的标准语言。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 2009年，对“绿坝”的抗议通过网络迅速传播，对那些长期以来不许公开讨论的事情在网上进行了生动的讨论，还转载了一位女子邓玉娇的证词，她在自我防卫中杀掉了试图强奸她的当地政府官员而被起诉。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 互联网给中国人提供了一个对一些敏感问题进行讨论并组织行动的机会。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 爱滋病问题的杰出活动家万延海（Wan Yanhai）在电子邮件和网络的帮助下，在北京成立爱滋病问题非政府组织，他说：“在过去十年来，（网络）对人们的生活产生了巨大影响，给了人们勇气来改变这个社会。”<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 共 产党一直对信息的力量有非常敏锐的认识。从它一开始统治起，它就禁止国外新闻来源，宣传部门的官员们则紧紧的控制着国家的每一份出版物和广播的内容。中国 在十九世纪八十年代后期有过短暂的自由，那时大学生们和精英成员被容许更多的自由聚会探讨。但是由于信息传播速度受科技及其它限制，那种自由是有限的。这 段时期以政府在一九八九年在天安门广场对学生的民主请愿进行镇压而告结束。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 不到十年后，网络进入中国，给中共带来了一个一直无法排解的难 题。中共官员知道网络是与外部沟通和进行商务活动的有利工具，但又担心其“危险”性。从一开始，他们就计划着如何控制网络。在一九九六年，他们规定所有网 络申请者必须到当地警察局登记。那时全国网络用户不到一百万，似乎还可行。但是很快，用户增加到数千万、数亿，再行不通了。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 在二零零三年，中共宣布了一项大型计划叫“金盾工程”，来管制互联网，明显的目的是让公安人员进行网上监控。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 今天，许多中共政府机构都在负责互联网监控，包括管制本地运作的网站，强迫他们过滤“非法”内容。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 但是当局每阻止一个异议，就带来更多的异议。加州大学柏克莱分校专研中国互联网的学者萧强（XiaoQiang）说：“有那么多人。他们能对一个很小的人群进行监控，但是这种方式肯定无法吓阻更多线上的声音。”<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 尽管中共政府对网络内容控制越来越内行、越来越快，但是它依然无法跟上网络的用途成番增长的速度。根据官方数据，在二零零九年上半年，平均每天有22万995个中国人开始使用互联网，也就是每分钟有153位新用户。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 互联网最终会打破中共的信息垄断，这是自由活动人士和中共官员们少数几个认识一致的事实之一。在十二月份中共政府的一份刊物上发表的文章中，公安部长孟建柱警告，互联网“已经成为‘反华势力’进行渗透和颠覆，并扩大他们力量的重要工具”。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 柏克莱分校的萧先生说：“其实，互联网是主流媒介。在互联网上报导的任何事，全国都会知道，中共政府的神经也会被触动。”<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 他说：“审查变得越来越尖端越来越强大，但是依然充满漏洞”。当政府试图封锁网络时，“主要的结果只是引起中国网络用户更多的抵制和反抗。他们在制造多得多的网络审查体系的敌人。”</div>

							
							
							
							
