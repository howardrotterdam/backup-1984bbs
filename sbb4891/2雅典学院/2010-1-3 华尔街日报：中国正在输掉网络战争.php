																											<div id="postmessage_415346" class="t_msgfont">我为什么要像戒毒一样戒掉QQ<br />
<br />
看起来也许是腾讯打败了国外企业，为咱们国家扬眉吐气。同时也依靠实力打败了国内的竞争对手，成为即时通讯的NO.1。<br />
<br />
但是现在的QQ实在是太强势了，以至于我们都有被QQ包围的感觉。被包围刚开始是喜悦，然后是淡然，最后就变成了恐慌了。这样强势的地位如果被滥用，后果自然是无法想像，数据、隐私各种乱七八糟的东西让我们心烦意乱。<br />
<br />
事实上我并不是一个接受新生事物很困难的人，甚至我对于各种类似XX威胁论嗤之以鼻。但是最近的一些事情让我无法沉默，腾讯QQ已经需要被规范了，大象无形至刚易折。<br />
无处安放的隐私<br />
<br />
你要知道的是，Google的大多数软件没有得到用户允许，是不会发送任何包含用户个人隐私的信息的。<br />
<br />
也许QQ的软件许可协议里面有这一条，也许没有，我不是一个精于法律条款的人，并有那么多的时间去研究那些纷繁错杂的许可协议。但是已经披露的某些信息让我们感觉很不安全，QQ也许比你想象中知道的多。<br />
<br />
昨天某用户在远景、深度等知名技术论坛发表了标题为“震惊！！用windows7自带的资源监视器之下的QQ可疑行为！”的帖子，发现QQ几乎触碰过我电脑里的所有文件，其中包括hotmail客户端、MSN、杀软、skype、firefox浏览器、私人文件夹、支付宝数字证书等，同时也给大家介绍了一篇2006年的老帖子“腾讯QQ如何监视你的聊天记录的”。<br />
<br />
这些消息让我们不寒而栗，相较于互联网公司的公关运作，我们更相信普通互联网用户的第一直觉。这位用户在向QQ客服投诉之后，腾讯只给出了业余且毫不负责的回复。<br />
<br />
腾讯收集用户资料数据存在以下几个问题：<br />
<br />
&nbsp; &nbsp; * 收集的数据是不是需要给出具体的列表<br />
&nbsp; &nbsp; * 列表是否能够经得起考验，即说一套做一套<br />
&nbsp; &nbsp; * 腾讯是否应该对网络流传的说法从技术上作出准确的回复<br />
&nbsp; &nbsp; * 腾讯的数据收集和使用是不是应该受到国家的监管<br />
<br />
当然也有朋友说，没准这就是官方的迷你绿爸，谁知道呢？不过腾讯这样的做法实在是让人心寒，曾经有句话叫做“在网上没人知道你是一只狗”，但是如果中国互联网公司都如腾讯这般行为，恐怕中国互联网的乱局就不远了。社会责任感、企业道德底线都是我们需要拷问腾讯的。<br />
用实际行动锄强扶弱<br />
<br />
要想戒除QQ并不是一件容易的事情，温水煮青蛙用来形容QQ的渗透是再贴切不过，现在我们已经很难将QQ完全剥离，因为我们的太多朋友都在上面。<br />
<br />
但是我们可以慢慢尝试其他优秀的服务，例如Gmail替代QQ邮箱。这样的做法有助于维持市场平衡，一家独大最后受害的只会是消费者，也许未来会有反垄断法来约束腾讯，但是至少现在互联网的垄断还是空白。<br />
<br />
更重要的是，腾讯这样空前强大的互联网巨鳄出现，将会遏制创新产品的出现。复制与抄袭之风将会成为互联网的主调，创新不能带来成功，因为腾讯只需要动动手就可以成功。<br />
<br />
他们有最好的技术团队，丰富的资源，很多的money。当然我这并不是片面的反抄袭论，腾讯的抄袭一直以来都是抄得还不错。<br />
<br />
但是在可选的情况下，我们还是尽可能支持一下腾讯的对手，让腾讯的成功不会那么完美。<br />
写在最后<br />
<br />
我从不仇视腾讯，也不是所谓的G粉，只是针对自己看到的东西写出自己的想法。在使用惯了移动之后其实也是可以尝试一下联通，当然如果开通携号转网之后成本会更加低廉。<br />
<br />
今天除了对腾讯的鄙视一下之外，也希望大家在可以的情况下顺手锄强扶弱，毕竟鹬蚌相争，咱们用户才能得利。<br />
