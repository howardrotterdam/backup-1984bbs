													<h2>对于民主的错误理解导致了我们忽视了很多该做的工作</h2>
						
						
																											<div id="postmessage_286881" class="t_msgfont">作者：新月<br />
<br />
民主就是普选权，这句话非常的可笑，但是被很多人坚持，甚至我必须说民主并不完全等于选举。有人说没有选举就不叫民主，这话和我上面的话没什么可矛盾的。民主是一套复杂的社会机制，同时这套机制需要复杂的社会条件才能支撑运转。首先我们无法复制任何一个国家的社会条件，即使我们的专制主义形态，也和苏联或者朝鲜有不同。我们中国就是中国，妄想把中国社会变成美国社会是可笑的，也是可悲的。那么如果我们承认社会本身是不可复制的，我们有什么理由认为制度可以复制？<br />
<br />
我们需要参考美国英国等国家一整套民主制度究竟以何种抽象的社会条件运转，包含了怎样的内在价值。我们可以说美国的民主制度建立在广泛的社会认同这一条件上，至于社会认同以何种形式，这要以具体情况为转移。美国人的认同可能是基于宗教，难道我们非要把基督教也引进中国来才行么？这可能么？但是曾经就有人这么主张过。<br />
<br />
社会认同是制度化的民主政治实行的重要前提性条件。我们需要思考如何发扬我们中国人本身能够接受的社会认同方式。美国人通过自己的宗教、意识形态以及地方自治，社区自治等方式完整的保留了美国人在建国之初的共和精神和认同感，这是我们需要认真思考的。如何构建我们自己的社会认同感，用何种机制保证这种认同感和参与性。这是关键性的问题。<br />
<br />
基层民主看上去没有任何的意思，因为它不能一夜之间决定中国向何处去。基层民主是缓慢的，问题重重，但是我们需要认识到基层民主是整个民主制度大厦的基石。基层民主的最重要的功能就是维系社会成员的社会认同感和参与感。可以说美国人并不是只有在两党选举和国会选举的时候才会感觉到自己“民主了”。他们生活的环境就是一个民主化的环境，广泛的社区活动和参与，培养了他们对于自己家庭、社区的认同，进而是对美利坚的认同。杜勒斯说，美国人之所以能打胜仗，因为他们心里都有家庭，他们知道为何而战。<br />
<br />
我曾经问过对于自己的社区活动和社区管理，有多少人愿意参与？有多少人愿意参与NGO的活动，周末做社工？有人说我们的社区没有组织活动。这就是问题，这就是需要努力的方向。这个方向似乎没有什么政治阻力，无非要求社区多办些活动，多看几场露天电影，让这些成为一种制度。但是有多少人在努力呢？有人甚至不知道自己楼上楼下住的是何许人也。这种对于公共生活冷淡的人，有什么理由认为这些人能弄民主？<br />
<br />
前一段时间深圳有公民在街头散发传单竞选人大代表，这很好。公民精神在哪里？就在这里。让别人知道你，让你知道别人。这就是民主的第一步。《报告》的统计数据显示，“在上一次居民/村民委员会选举中的投票情况”，有56.3%的人没有投票，只有26.9%的人是自己主动去的，还有16.8%是被单位或者街道干部要求才去的。这说明我们对于自己的社区认同感很低，更说明了我们的公共事务的参与热情并不高。当然，有人会说居委会没有权力，自然不重要，没人管。但是人大代表的选举，我们都知道选票上有一栏是空白的，有多少人像那位深圳公民一样争取过大家支持？<br />
<br />
是的，有人说有猫腻，我想就算是能进基层人大，能发出的声音也是微弱的。但是我说过，认真对待权利，并不是为了某个结果，而是一个共和的过程。</div>

							
							
							
							
