													<h2>茅于轼：国家需由精英而不是大众治理</h2>
						
						
																											<div id="postmessage_286236" class="t_msgfont">茅于轼：国家需由精英而不是大众治理 党要控制住利益集团<br />
2009年10月16日 南都周刊<br />
今天，在这一新时代的当口，我最为关心的是，以后的三十年或者更长的时间里，中国的经济和政治环境将是怎样的一个状态？我认为这个问题，兼具理论和现实双重意义。<br />
<br />
建国后的这六十周年，很明显地分成两个阶段，前三十年和后三十年。虽然都是在中国共产党的领导下，但前三十年因为搞了个阶级斗争，经济上一塌糊涂。后三十年，我们采取了市场经济这样一个手段，社会面貌得到了根本性改观。<br />
<br />
今天，在这一新时代的当口，我最为关心的是，以后的三十年或者更长的时间里，中国的经济和政治环境将是怎样的一个状态？我认为这个问题，兼具理论和现实双重意义。<br />
<br />
首先要说，市场经济制度是一个很成熟的制度，它首先是在民主国家发生的，中国的市场经济制度开始在一个什么样的起点之上呢？是在后三十年的初始，在此之前，中国的老百姓没有自主选择工作的自由、没有旅行的自由，大家只有干活儿的份儿。所以，后半段的这三十年，和前三十年是完全不同的。<br />
<br />
《中华人民共和国宪法》明确指出，无产阶级先锋队就是共产党，那么，专政下的市场经济，有没有可持续性，能不能长此以往、持久不变？全球还没有一个专政的市场经济国家，中国独一无二。所以，这是个理论问题，也是个我们正在面临的现实问题，许多国际学者在反复讨论。<br />
<br />
我最近写过几篇文章，我对此的看法是，一个社会要想稳定，需要有两个必要条件：第一，国家需要由精英而不是由劳苦大众治理。从邓小平迄今的三十年，中国的精英治国趋势越来越明显，政府官员、公务员，差不多都已是大学以上文化水平，而且具有硕士、博士学位的官员越来越多，从国外深造归来进入国家管理岗位的也越来越多。这满足了我所谓的第一个条件。<br />
<br />
但是，精英治国并非意味着永远不会出问题。一旦国家发生革命性状况，精英分子必然让位给手里有枪杆子的人，但战争的精英，不是治理国家的精英。越来越走向精英治理的中国，仍然存在这样一个风险：一旦发生革命性冲突、动乱，精英是不是要被迫让位？<br />
<br />
社会稳定的第二个条件，是实现国家治理的精英分子，优先考虑的必须是底层群众的利益。如果精英考虑的都是自己的利益，那么，这个社会是靠不住的。说明白点，如果精英勾搭起来，形成利益集团，以自己或自己小集团的利益为优先考虑，那么这个国家和社会是危险的，而这种苗头，在现今中国，已经出现并日成规模，这是我很感忧虑的事情。而这个问题，是关系到我们无产阶级专政下的市场经济能不能继续下去的核心因素。<br />
<br />
如果党能够控制住这些既得利益集团，不让他们将自己的利益凌驾于群众利益之上，或者，妥善安排出一种制度，使得这些利益集团之间能够实现相互制约，进而达到能够充分考虑底层群众利益之目的，那么，稳定与持续是没有问题的，否则，不好说。<br />
<br />
抗战结束后，从1945年到1946年，有一段和平时期。这一年里，老百姓的生活尽管没有完全恢复到战前水平，但得到了一定程度的休养生息，经济上升得很快。八年抗战，苦得很，大伤元气。抗战胜利后，大家都能吃饱饭了，也能吃上点肉了，却开始迎来持续三十年的无产阶级专政和阶级斗争。这三十年里，吃饭住房都成问题。<br />
<br />
后三十年，中国的变革是空前的。从中国到外国，从历史到现实，是突飞猛进、奇迹不断的三十年。现在我们与全球其他任何一个国家相比都不弱，中国的基础实施建设，全世界第一，钢产量全世界第一，肉产量全世界第一。小康社会这个目标，在我看来，差不多已经实现了。这是几千年来，从未有过的大发展。<br />
<br />
这当然是一个奇迹，这个奇迹，需要一个解释：为什么中国成功了？世界上穷国多得是，为什么他们没成功？中国现在是市场经济，全世界市场经济国家很多啊，为什么惟独中国成功了呢？<br />
<br />
我觉得，根本的原因是中国采纳了一个市场的制度，而这个市场制度是以交换为特征的。现在我们财富这么多，不是因为劳动多了，而是交换多了。而且，中国是一个专政的市场经济国家，他们都是民主的市场经济国家。专政的市场经济与民主的市场经济有什么区别呢？专政情况下，国家可以动用巨大力量集中解决一些问题，比如基础设施建设。这些问题，通过市场手段也是可以解决的，但需要很长时间，五六十年，八十年，甚至上百年才能搞起来，而我们用了不到三十年的时间就搞成了。这就是专政的力量。反观印度，是一个民主的国家，也实行市场经济制度，但它的公共设施简直糟透了。<br />
<br />
还要提及的是，这些年，共产党保持了一个稳定的货币政策，中间除了1994年有个短暂波动外，整体看来，人民币甚至比美金还要稳定。另外，中国的宏观经济也是比较稳定的，没有大起大落，都保持8%以上的经济增速，甚至到了12%-13%。所以，这些稳定状态的存在，使得大家不用分心去想别的事情，这是中国经济得以成长的重要因素。这一点，不能不承认。<br />
<br />
有些人说，专政不好。确实，现在看来，专政的弊端日渐突出，但大家也要客观地承认专政有它的好处。我想，大家也不用拿“威权、中央集权”来替代“专政”这一说法，因为，我们宪法上明确写的，就是无产阶级专政。我曾先后几次提议，要将专政这一提法改一下，但一直没改。看起来，专政还是要继续下去。（记者 齐介仑 采访整理）</div>

							
							
							
							
