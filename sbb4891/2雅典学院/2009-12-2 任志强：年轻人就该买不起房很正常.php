<br />
　　中国40岁以下购房比例全世界居高<br />
<br />
　　任志强说,如果从发达国家来看,30岁以下的人没工作几年,就靠自己独立收入去买房的几乎为0,非常少。如果未来收入不足以满足住房按揭贷款或者购房支付能力的话,都买不起房。<br />
<br />
　　中国的传统习惯让大家不得不买房,所以年轻人购房比例居高。也就是说,在中国40岁以下的购房比例接近于60%―70%,这个比例在全世界都没有过,原因就在于中国的房子太便宜了,原因也在于中国有一个巨大的50年左右的福利分房制度,所以让大家更愿意把拥有私有住房来作为自己的梦想。<br />
<br />
　　任志强说,在美国有一套房有一部车是梦想,如果在美国这样的发达国家都称之为梦想,在中国30岁左右的人就实现了这一梦想,应该说中国的消费能力和消费观念,已经远远超过了一些发达国家或者很多很多的发达国家。<br />
<br />
　　“居者有其屋”应改为“居者有其所”<br />
<br />
　　任志强认为,我国大部分的人都是在40岁左右才分到房子,30岁以下的人是没有房子的。结婚常常是住集体宿舍,合住房子是非常普遍的现象。任志强说:“为什么现在的年轻人要一下子超越自己的父辈呢？这里有一个最大的错误观念,就是住房体系中,总是说居者有其屋,按道理说,应该是居者有其所,有地方住就行了。 ”</div>

							
							
							
							
