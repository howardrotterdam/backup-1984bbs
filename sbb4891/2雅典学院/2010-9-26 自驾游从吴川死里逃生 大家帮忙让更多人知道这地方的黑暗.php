<br />
首先，我要谢谢你看见这帖子，这意味着我有机会让多一个人知道我所经历的这个恐怖的真实故事，而避免让更多的人陷入这样的危险之中。我曾经一度不想去回忆这些一直痛苦不安的细节，但是如同朋友说的，既然它是真实残忍的，那就应该让更多爱好旅游，也有可能经过那个地方的朋友们知道。所以，请你耐心看完并且让你身边的朋友也知道，好吗？<br />
<br />
2010年8月20号，我，还有一个男生刘和另一个女生青一起拼车自驾游去上海，途中需要经过广州，由于种种原因我们没有来得及在傍晚时候定到合适的旅店，所以刘一直开车开到凌晨4点多，我和青早已经睡着了，但是我还是能感觉到他把车子停到了一个有光的地方，想打个盹再走。后来我知道那地方是湛江市的吴川县覃巴加油站。<br />
<br />
忽然，我听到了暴雨敲打车窗的声音，睁开眼睛一看，不是暴雨，而是有3个人正站在我们的车子外面疯狂地砍着驾驶室的玻璃，我本能地爬了起来趴在放低椅子在睡觉的刘身上，左手想把玻璃碎片挡出去，嘴里的叫喊声和玻璃碎裂的声音刺耳得划破了宁静的夜空，<br />
<br />
混乱中，刘下了车，他大声说着，“东西都给你们，都给你们，你们都拿走，都拿走！”但是歹徒并没有因为他不反抗而放过他，其中的两个把他赶到了加油站的另一边去。剩下的一个就拿着刀站在副驾驶室上看着我和青，我们两个像被定格了，一动不动。世界也停顿了，直到那两个歹徒再跑回来将后排座位的玻璃都敲碎，然后将车里所有的东西都洗劫一空，再跳上他们自己带来的货车扬长而去。<br />
<br />
血腥暴力干脆利落。和我们在电视上看见的抢劫杀人情节一样，只不过这是真实的，主角就是我们自己。而目睹这一切发生的除了我们自己，还有加油站的两个戏剧化出现的工作人员，他们在车子开走后及时地帮我们报警，我也赶在刘捂着肚子倒下去的前一秒拦腰抱住了他一起跌坐在了冰冷的地面上。<br />
<br />
我不知道他伤在了哪里，但是我却潜意识觉得我应该要抱住他的胸口，也许这样做他的血不会流那么快，现在想来如果不是那么做了，也许我再也没有勇气坐在这里和你们叙说这恐怖到不真实的经历。我觉得真的很有必要，我们每个人都应该学会急救的知识，而不是像我这样，根本不知道自己做得是对还是错，而如果我做的是错的，他就已经被我间接害死。<br />
<br />
在绝望中等待120到来的半个多小时里，我的双手感觉他的生命体征慢慢在流失，而我也像个被现实逼疯的神经病一样，到最后只剩下了自言自语在清冷的空气里游荡。关于那一刻，我能告诉你们的感觉就是，人，在最紧急的时候是没有情感没有眼泪的，它们已经渺小到没有存在的空间，当你痛苦的时候还能流出眼泪说出感人肺腑的话，恭喜你，一切还没有到真的需要绝望的地步。<br />
<br />
终于，医生在他身上最后一滴血流干前把他推进了手术室，而我这个活着的血人也在手术室外面经历了仿佛一个世纪之久濒临崩溃的煎熬。我亲眼看着医生把他因为被血浸透而无法脱掉所以剪成一条条破布的衣服扔掉，也看着医生捧着一脸盆的血放在我面前，告诉我，这就是在他胸腔里拿出来的，“他会不会死？”“过了今天晚上才知道！”这就是我和他主治医生没有一丝温度的对话 。而就在早上3点前，他还在和我说笑话。医生的回答让我的心跳停止了很久，直到有人在我肩膀上拍了一下我才知道他还有呼吸只是还在病危中。<br />
<br />
我们是如此的倒霉却又如此幸运。直到他脱离了危险期，医生才告诉我，其实他们根本没有把握救活他，因为他几乎整个身体的血液都流掉了，但是他还是靠着顽强的意志力挺了过来，这不得不说是上天给的另一种恩赐。<br />
<br />
----<br />
<br />
故事发生到这，大家应该就知道了，这并不是普通的案件，而是特大的刑事案件，我们只是经过这里的游客，而且是停在24小时营业的加油站被人打劫，但是我们不单是被人打劫而且还被故意杀人。<br />
<br />
----<br />
<br />
插播两个故事，在刘住进医院的那晚，我在半夜去洗手间的时候，看见了后面树林里的一场凶杀案，一共3个人，我眼睛近视，只见到他们在推来推去，但是到了第2天清晨的时候就有警察在那里办案了，有个20出头的年轻人被杀了，如果我不是近视，可以想象得到，我做恶梦的时间又得延长一段时间了。<br />
<br />
然后在他住院的第2天晚上，走道里安了新床给一个也是刚做完大手术从黄泉路上被救回来的人，一问，又是被人抢劫砍了，不过他是本地人，所以说那里的歹徒对待受害者是一视同仁的，不分亲疏。<br />
<br />
----<br />
<br />
下面我就来说说我们的案件是被怎样对待的，让你们心里对这个地方的治安心里有个底。<br />
<br />
首先，在他手术期间，有个人来问了我几个问题，比如几个人打劫，我们几个人，你们也一定能想得到的例行公事的那种对白，回答完后他就走了<br />
<br />
第2天，又来了几个人说是覃巴派出所的，问了同样的问题，然后加一条说他的车子被其他部门拿去检验了，然后又走了<br />
<br />
由于刘还不能回答他们的问题，所以他们得等过一天再来<br />
<br />
再过了一天，他们回来问了刘同样的问题，然后我们将这些同样的回答回答了一次又一次<br />
<br />
这是为了配合他们的工作，如果可以我希望我们能给得更多更仔细。但是他们说，我们只需要回答他们问的问题就可以了，看样子他们对自己的破案能力也是有信心的，不然不会所有的人都问统一问题，甚至不需要我们多一点点疑问。所以我们对他们是有满满的期待的<br />
<br />
----<br />
<br />
在他因重伤住院的日子里，除了录过几次一样口供外，从来没有人来主动告诉过我们关于案情的任何发展，但是我心里还是有抱有希望的，为民除害是他们的职责和本领，对于如此残忍的凶手，我们应该给他们点时间。<br />
<br />
但是，我发现原来我也是好傻好天真<br />
<br />
以下是一个警察对我说的<br />
<br />
“你们知道不知道？为了给你们录口供？我有两天都没有吃到中午饭？，”<br />
<br />
“你以为我们工资高吗？拿那么一千多点，你们以为我们不会怕吗？拿着枪和人开战的时候，”<br />
<br />
“ 你们知道每天有多少这样的案件吗？你们的已经够重视了，连湛江的刑警大队都出动了！……“<br />
<br />
我当时心里听了是5味俱全，啊，真的好委屈他们，他们居然为了我们做了这么多事情，连午饭都顾不上吃，还把市里面的头都惊动了，不就是个破杀人案吗，这里几乎每天都发生……<br />
<br />
“那我可以把这个事情放上网去，提醒下其他的旅友让他们注意下吗？”我弱弱地问了句<br />
<br />
“ 那你们还想不想破案了？这么做不等于是通知凶手我们在找他们吗？你们这么做不是加大我们破案的难度吗？……”<br />
<br />
我只能无语，那我们就再等等吧。我对刘说。我们得理解他们的难处。<br />
<br />
----<br />
<br />
过了几天，我拿我们的病历去给法医做鉴定报告，他进了房间后把病历往旁边一放，“先放这里，我马上急着要走要赶着去看尸体，还有几具尸体没有看呢！”我怀疑我听错了“几具？”我的舌头打了个结“一天到晚忙死了，你的鉴定报告还要等星期天晚上抽个空才能做！”我乖乖地退了出门，和人家的尸体相比，我们的报告确实是没有那么重要的。就算我们也差点丢了命。<br />
<br />
回来后为了了解状况，我先拨通拨覃巴派出所的电话，结果是案件转移到了湛江刑警大队，还真的有人在做事情呀，心里塌实了一下，又拨通了湛江刑警大队负责人电话，一听到问进度，他给了个回答“你打个电话去问问110，看下你们是什么时候报的警？”我当时几乎要晕过去，敢情上回给我们录口供的同志根本就没把我们的案件告诉现在的负责人，那派出所又怎么说是他负责的案子？而他又把责任推到了只是接受报警的110？<br />
<br />
我很清楚明白的就是，这就是个混帐的团队在运转着一团混帐<br />
<br />
覃巴派出所把案子推给湛江刑警大队，湛江刑警大队推无可推后只能往110身上推，因为他总要找个人来解释，为什么，案发到现在过了10天，不仅连凶手的毛都没抓到一条，更没有办法解释出到底是谁在一直处理这个案子？破案？亏得我还以为他们需要的只是多一点点时间！<br />
<br />
还是最后一回载我去派出所的一个叔叔给了我一个答案“你指望他们破案？你没有听过警匪是一家吗？你们能把命保住离开这里已经是很幸运了，在这里别说你们外地人，即使我们本地人，晚点都不敢出去搭客，我都被抢过几回。！”“这可是杀人案！”“死几个人算什么，在这里死个人就等于向大海投个石头，你就听我的，快走快好，老实说，就算我们知道谁是凶手也是不可能告诉你的，更不可能告诉警察，我们还得在这里生活，！”他叹了口气再没有说话。<br />
<br />
听了他的话，我心里一片凄凉，我愤怒，我无助，但是都没有用，在这里，没有我们平时在课本里耳朵里听到的正义也无法去对谁呐喊，让他们为我们做主，帮我们报仇。在这里，警察只是像你我普通人一样要按时上下班的职业，不管是抢劫还是杀人也不过是可以用时间淡化的一份笔录，只要按上你的指印就代表一切已经结束，他们听过你的故事就已经做得够好，至于你有没有因此留下永远的心理阴影，有没有命活着回家，就看你的运气了！<br />
<br />
刘都出院去修养了，我还是没有死心，9月15号的时候又打了个电话问进度说如果还没有一点进度，我就真要把这事情放上天涯了，让更多的人避免遇见这样的危险。这下他火了，“你不要问我，我不知道这事情，你去问派出所，问他同意不同意……！”“我管不了那么多，我现在是通知了你。别到时候说我防碍你们办案！”没有等他回答我便挂了电话。<br />
<br />
如果我还会指望他们能找到凶手的话，那真是这个世纪最大笑话了吧！<br />
<br />
----<br />
<br />
以上就是我在广东湛江吴川县所经历的全部恐怖回忆，自我发泄也好警示也好，希望多少对你们有点点帮助，第一，尽量避免去吴川那个地方，要去也不能在晚上，最好有多远就离开多远<br />
<br />
第二 ，如果真的那么倒霉出了事情又没有人管，第一时间通知社会媒体，他们的力量应该多少帮到一点点，千万别想着你会阻碍别人的路，那样就会断了自己退路<br />
<br />
第三，希望你们永远也不会遇见这样的事情，我们大家都好好的健康生活<br />
<br />
以上所有的所有我都可以以人格担保绝对事实，都是我个人经历。个人感想，与其他人无关<br />
