													<h2>毛泽东究竟有什么功绩呢？</h2>
						
						
																											<div id="postmessage_791779" class="t_msgfont">《毛究竟有什么功绩呢？》（2009-3增订版）<br />
<br />
原帖转自：凯迪社区→猫论天下→史海钩沉<br />
<br />
原帖作者：东风51<br />
<br />
文章提交者：东风51加帖在史海钩沉【凯迪网络】<a href="http://www.kdnet.net" target="_blank">http://www.kdnet.net</a><br />
<br />
毛犯过错误，许多是重大错误，这点恐怕已是大家共识，但部分人认为毛也是有功的，应客观完整地看待和评论毛。那么毛究竟有哪些功绩？我们不妨逐个分析一下。<br />
<br />
一，推翻三座大山，让中国人站了起来。<br />
<br />
这是最常见到的，毛的最大功绩。我们不妨来认真分析一下，所谓“三座大山”即指帝国主义、封建主义、官僚则本主义。在二战前，中国许多地方被帝国主义列强控制，东北和台湾割让给日本；香港由英国掌管；澳门由葡萄牙掌管；上海、天津、青岛、大连、武汉……有许多外国租界地，等等。<br />
<br />
1，推翻第一座大山――帝国主义<br />
<br />
（1），东北的收复是随二战结束，日本投降而回归中国的，很难算做毛的功劳，毛从来没到台湾打日本，还不是一样回归了中国？让日本在二战中决定性失败的是太平洋战场，中国战场并没让日军伤筋动骨。即便在中国战场上，正面作战的是“国军”，东北战场上毛自己在《毛选》中都说“日本投降的决定因素是苏联参战。”（见《毛泽东选集》第四卷，《抗日战争胜利后的时局和我们的方针》），更不必谈毛没打过的南方日军。因此，毛在抗日战争胜利中究竟起到多大作用，十分值得商榷。<br />
<br />
（2），其它地区的回归。台湾回归中国大概很难算做毛的功劳，香港、澳门都是在毛去世后回归的，也不必谈了。余下的就剩几个大城市的外国租界地的收复了，如果这个收复属于功劳的话，是应算给毛的。但一个国家的强盛主要取决于自身的发展，几块租界能对中国整体走向产生多大程度影响值得怀疑。我们今天引进的外资、企业和各种办事机构恐怕比当年不知多几倍，还不是照常按自己的道路发展进步。况且台湾地区，我们当年并没去没收外国企业，现在台湾的主体经济还不是由台湾人自己主导，并没沦为列强殖民地一说。<br />
<br />
此外，有网友谈到除香港，澳门和东北苏军基地外，外国租界地45年就已经消失了。还有网友说上海，天津等大城市租界取消的事主要是国民政府和汪伪政府解决的。二战是英美法的租借和中国是同盟，通过外交途径解决的日本的租借是汪伪政府负责交涉取消的。不过这事我并没认真查资料，不作为根据。顺便说说而已，大家仅做个参考，更希望了解租界问题的朋友发表意见。<br />
<br />
附：某网友跟贴的资料：<br />
<br />
中国人民收回外国租界的年代<br />
<br />
1919年收回天津德。奥租界和汉口法租界<br />
<br />
1924年收回苏联放弃的前俄租界<br />
<br />
1927年收回汉口九江英租界<br />
<br />
1929年收回天津比租界和镇江英租界<br />
<br />
1930年收回厦门英租界<br />
<br />
1943年收回天津广州英租界及英美比在上海和厦门的公共租界<br />
<br />
1945年收回在中国各地的日租界<br />
<br />
1946年收回上海天津汉口和广州的法租界及法国在上海厦门公共租界的权利。<br />
<br />
1947年收回天津意大利租界及意在上海厦门的公共租界的权利。<br />
<br />
至此帝国主义国家在中国设立的租界被全部收回。<br />
<br />
2，推翻第二座大山――封建主义。<br />
<br />
国共内战时，毛向全中国人民表明自己未来的建国方略是建设一个类似于英美那样的民主制度国家（这在《历史的先声》中体现的非常明白）。这要比蒋介石的独裁政府不知好多少倍！这让中国人看到了个光明的未来，使得绝大多数中国人，包括许多家境优越，追求民族进步理想的富人都站到毛的一边。这就是为什么毛能推翻蒋的最根本的原因。可以说，毛那时的承诺顺应历史大潮，代表了中国最广大人民群众的根本利益。但说归说，后来毛建立的制度比蒋介石的独裁政府民主多少，恐怕就不用多说了吧。<br />
<br />
从形式上，毛建立了个“新中国”，但这个新的国家制度究竟比原来的中华民国制度好在哪却越来越让人看不清。毛跟随苏联搞的“计划经济”让大陆被台湾远远抛在后面。所有的按毛的标准属于“社会主义国家”的国家，无一例外全部经济失败。<br />
<br />
死了数以千万计的生命，给整个社会经济和人民的生活带来难以估量的损失。以这样沉重的代价换来的这个“新”东西究竟是功是过，大家评说吧。<br />
<br />
3，推翻第三座大山――官僚资本主义。<br />
<br />
毛时期，人民貌似当家作主人，但并没有任何实质性的政治权利。有人说人民可约束官员，但你能约束的只能是毛定义的“走资派”，你约束个革委会主任来看看？你立刻会被革命人民“识破”，由“革命”的一伙扔进“反革命”一伙里去，从95%一堆里骤然变成5%的一员。<br />
<br />
说当年群众热情高，你敢不“热情”吗？知青们被人赶到农村（象不象被卖到‘黑窑’？哈）还必须兴高采烈，真是被人卖了，还得帮人家数钱。高高兴兴地数。他们是自愿的吗，为什么打倒四人帮后又都回城里了？<br />
<br />
二，毛使中国国际地位提高。<br />
<br />
真是这样吗？须知二战结束时成立[联合国]，中国就作为创始人而成了五个常任理事国之一。要谈这方面功劳，貌似蒋介石更大。新中国成立后很长时期游离于国际社会之外，直到后来跟苏联闹崩才急忙与西方国家建交靠近以求平衡苏联的威胁。交的一些“兄弟国家”也没几个不翻过脸的。<br />
<br />
也有人认为毛不怕任何敌人，敢碰硬。跟美国打、跟印度打、跟苏联打……，打出了地位，打出了军威，大长了中国人民的志气，大灭了帝修反的威风。但我个人倾向于把这些算毛的“特点”，能感到“与人斗其乐无穷”的人世界上不多。最多算个“特长”，不算“功绩”。拉登也把许多国家整得害怕，朝鲜的原子弹也让许多国家胆战心惊，但阿富汗和朝鲜的国际形象并没因此提升。老实说跟四邻都打遍了有啥好处，在保证本国利益情况下，越少打仗越好。<br />
<br />
三，创造“两弹一星”，成绩惊人。<br />
<br />
以前也觉得这挺了不起的，世界上没几个国家有原子弹！后来才发现，没有的并不一定不会造。巴基斯坦会造，朝鲜也会造，看来这玩艺是不是“高科技”真值得怀疑。<br />
