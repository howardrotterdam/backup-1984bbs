<br />
再看看当时整个社会腐败。在毛时代计划经济体系下的腐败形式和今天市场经济体系下的腐败形式，是有很大区别的。<br />
<br />
毛设计了一种特殊的等级制度。其一是城乡二元化，其二是8级工制和24级干部制。城乡二元化，将农村基本生活素质降低。此后，大量农民就代表了中国最贫困的阶层。一个农民只有通过参军、上大学、当工人等途径“升级”，才能拥有城镇户口。8级工制度和24级干部制，是一种终身制。一个人在等级里只有不断努力，拼命钻营，才能“升级”。虽说工人、贫下中农当家作主，但8级工再怎么牛皮，也难获得15级干部的物质享受。因此，社会上又形成了农民-工人-国家干部三种身份间的巨大差别。你是农民，要想办法变成工人，你是工人，要努力使自己成为“国家干部”。一个够“级别”的干部家里可公费装电话，出门有汽车，出差乘软席或飞机。这一切都是身份和级别的象征，普通老百姓是与电话、软席、飞机无缘的，即便你有钱。官员的食品、烟酒、生活用品都有“专供”，完全不同于普通百姓一切凭票供应的贫苦境地。当时在东北有段顺口溜就是这种差别的写照：省【群英】，县【牡丹】，一般干部【迎春】烟，工人阶级大【握手】，傻B青年【农丰】烟。（【农丰】牌香烟9分钱一包。呵呵）<br />
