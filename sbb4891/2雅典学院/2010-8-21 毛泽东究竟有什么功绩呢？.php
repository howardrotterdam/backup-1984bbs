<br />
很多朋友谈得最响的观点是毛时代无腐败。毛泽东曾经杀了刘青山、张子善，此后未出什么大贪官，社会变得廉洁奉公，所以没有了腐败；毛泽东坚决消灭娼妓，社会没有了那些污七八糟的东西，所以没有腐败；毛泽东活着的时候，没有贫富差别，所以没有腐败；毛泽东在世的时候，工人阶级、贫下中农当家作主，谁敢腐败？――说毛泽东时代没有腐败，大致就是这些个理由。事实果真如此吗？<br />
<br />
让我们先从毛泽东本人说起。说毛生活简朴，困难时不吃红烧肉（资料显示改吃西餐了，非常精细、高级，《毛泽东遗物事典》94―96页，作者：韶山毛泽东同志纪念馆编，顾问：汪东兴、张玉凤出版社：红旗出版社，书号：ISBN7-80068-972-7/A.19），睡衣带补丁（资料显示补衣服花的钱一点不比买件新衣节省钱，《毛泽东和他的卫士们》第二十四篇，中央文献出版社，书号：ISBN7-5073-0504-X）。可光个韶山滴水洞别墅就占了一大片风景区，还进口不少苏联的设施和用品，须知这别墅是在全国人民忍饥挨饿的1960年兴建的，别处还有不少别墅就不提了。毛的香烟要专门制作，这一支香烟要抵上多少红烧肉啊？毛的瓷器也要专门制作，这一只饭碗要抵上多少件睡衣啊？<br />
