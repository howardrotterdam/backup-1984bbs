<br />
毛曾不断提倡“破除迷信”，打破“等级制度”，可为了维持计划经济，却又非要等级制度不可。而且这种等级制度的设定比印度几千年前的种性制度还糟糕。为使社会不被这个等级制度管得太死，也为了政治需要，毛时代又设计了一种运动明星跃进制度。如果一个人的“政治表现”绝佳，那么，他就可以突破等级的约束。这样，王洪文、陈永贵、张铁生、黄帅……那种人便有了火箭升级的机会。<br />
