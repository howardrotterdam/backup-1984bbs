<br />
全国当时八九亿多人都在争取“政治表现”，那就不可能所有的人都能因“政治表现”达到“升级”的目的。那么，正如千万人过独木桥一样，丑恶、邪恶、可恶就只能在“政治表现”背后暴露出来。相互间拆台、无德无义无人性、道德极端败坏等就都成为那个时代的常态。而这些，不是腐败又是什么？！但是，由于“政治表现”需要较好的掩饰技术，而媒体、社会舆论通过严格的政治控制，大家又只能高喊“现在形势一片大好”，连歌都是声嘶力竭地狂叫“就是好啊就是好”，因此，一种虚假的谣言在那个时代就已在中华大地散布。很有稍有理智的人都知道，那种争取“政治表现”背后的腐败往往是灭绝人性的。<br />
