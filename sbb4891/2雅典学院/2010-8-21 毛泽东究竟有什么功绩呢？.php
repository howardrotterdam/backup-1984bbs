首先，统计数字的权威性要来自无数的原始基础数据的权威性。国家统计数字是各地方，各部门，各工厂，各乡村，个人各户原始数据的积累。文革期间各地所有机关基本瘫痪，包括公检法部门都被砸烂，全国人民每天除了学毛著、写大批判文章、揪斗阶级敌人，就是武斗。各地开着宣传车，拎着棍棒上街，厉害的地方开着坦克，拎着机关枪。在这种状态下，统计部门能统计出精确的数字十分令人怀疑。<br />
<br />
其二，是先有统计部门的数据，然后再有宣传部门的报道宣传；还是先有宣传部门的宣传口径，然后再有统计部门的数据跟进做支持呢？“大跃进”时统计部门能根据宣传部门的需要“统计”出亩产万斤的数字，那么以阶级斗争为纲，一切为无产阶级政治服务的文革狂热年代，统计数字又会怎么产生呢？<br />
<br />
再有，人类社会总体是向前发展的，每一时期经济、技术都是在前阶段的积累基础上进步的。慈禧太后的晚清再腐朽，科技方面也比康熙盛世发达，毕竟光绪有自行车了，而康熙没有。所以用这样方法自己和自己比“进步”实在是没多大价值。<br />
<br />
那怎么比才能看到国家经济是进步了，还是退步了呢？我想，比较国家在世界上的经济比重地位是上升了，还是下降了应该是比较合理的方法。这好比一个学生，他一年级时年级排名第十名，到三年级时年级排名下降到第二十名，我们就可以说这个学生学习成绩退步了，尽管他三年级时又学会了5X6=30，而一年级时不会乘法运算。<br />
<br />
那么请看一下毛时代中国经济占世界经济的比重较旧中国时期是变大了，还是减小了吧：<br />
<br />
1913年（中华民国初期）中国经济占世界经济比例：8.9%<br />
<br />
1950年（中华人民共和国建国初期，也可认为是中华民国末期）中国经济占世界经济比例：4.5%<br />
<br />
1973年（文革中后期）中国经济占世界经济比例：4.6%<br />
<br />
1998年（亚洲金融风暴后）中国经济占世界经济比例：11.5%<br />
<br />
（详见附件④）<br />
<br />
由此可见，从1949年到1973年，中国经济占世界经济比例由4.5%变成4.6%，基本没什么增长。而1949年的中国是刚刚结束多年战乱的状态，经济受到长期战火的摧残，毛时代的经济却只能和这样一个时期一样地位。<br />
<br />
