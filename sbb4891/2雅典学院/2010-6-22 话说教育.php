													<h2>贴一个旧日志</h2>
						
						
																											<div id="postmessage_698939" class="t_msgfont">杂说师说<br />
(2007-09-26 21:51:35)<br />
 <br />
 <br />
多年来，就想为《师说》写点东西。但是一直以来，只是在课堂上，引导学生体悟了便罢，除了六七年前的戏作文言《师说新语》外，关于《师说》，始终未着片言。<br />
 <br />
韩愈的《师说》，最精粹的东西，我觉得，不是“道之所存，师之所存”，不是“闻道有先后，术业有专攻”，不是“弟子不必不如师，师不必贤于弟子”，这些明见，固然有其不灭的光，可以让今人看到古人的睿智。但是，使韩愈的《师说》千载流传的还不是这些。<br />
最精粹的地方，是这些：<br />
“师者，所以传道受业解惑也”<br />
“彼童子之师，授之书而习其句读者，非吾所谓传其道解其惑者也！”<br />
仅这两处话语，便足以让《师说》彪炳千秋的书册。<br />
 <br />
韩愈的贡献，在于他卓越的判断。他为教师，做了一个明晰的定义，并且将概念的内涵阐述得一清二楚。千载之后，读到这些简明有力饱蘸了感情的句子，油然而生的是复杂难陈的感慨！<br />
时代是在向前的，但是，很多本质的东西，其实不过是在回归原点。而且，回归的路途，曲折艰难。我们这个时代，一旦有人提出一个常识，会博得激动地喝彩，这样的时代，真的不知是该欢喜还是该悲哀。需要承认的是，这样的时代，人要想活得比较接近人的常态，最需要的有两点，一个是勇气，一个是耐性。而耐性，往往是最重要的。比如教书的人，议论课堂的预设和生成等诸如此类的问题，有懂得最基本常识的人，挺身而出，掰开了揉碎了地讲解分析，枚举事例，鞭辟入里，在有接受砖头与鲜花比翼齐飞擂鼓共喝彩连成一色的勇气之外，最不能缺少的是谆谆教导诲人不倦的超常的耐性。这样的耐性，对于孩子，我还是有的，很有。可是……<br />
突然发现，我扯远了。<br />
呵呵，总是记流水帐，不小心，意识流了。<br />
还是就此打住，回来，回到《师说》上来。<br />
 <br />
韩愈为老师下了一个相当完备的定义，“师者，所以传道受业解惑也。”<br />
老师要完成三个方面的工作：传道，授业，解惑。<br />
什么是“道”？“道可道，非常道”。韩愈排佛老，但是，解释他这个道，还只有这六个字最恰当。<br />
什么是“惑”？“人非生而知之者，孰能无惑？”韩愈从《论语》里化出来的这句话，最能说明“惑”的内涵。<br />
什么是“业”呢？此处，大有嚼头！韩愈在此，作一个概念剖析，他说“授之书而习其句读者，非吾所谓传其道解其惑者也！”既然，“非吾所谓传其道解其惑者也”，那么，自然这“授之书而习其句读者”就是“授业”之师了！那么，“授之书而习其句读” 之类的行为，就属于“授业”的性质了，那么，“书”与“句读”之类，就属于“业”的范畴了。<br />
然而，韩愈对于这授业之师，是当作授小之师的。所谓“句读之不知惑之不解或师焉或否焉小学而大遗”是也！（此处不加点读，特请授业之师辛苦则个）<br />
诚然，韩愈作此文，是在慨叹“师道之不复”。<br />
但是，我们今天读此文，大可不必拘泥他的写作目的。毕竟他写作之时代“久矣”！<br />
 <br />
抽取这两句，做了这样的分析。<br />
我只是想到，如今，有多少授业之师，在勤恳严厉地授业？有多少教师，还记得师者，所以传道受业解惑也？有多少教师曾想过，授业，是介乎传道与解惑之间的一架桥梁？有多少教师，在为自己一切为了分数为了分数的一切为了一切的分数的种种行为寻找各种不得不如此的逼不得已的借口的时候，还会清夜自问――我真的只能如此吗？<br />
 <br />
监狱里，习惯让犯人管犯人，总是能发挥其恶之极致。<br />
有时候，经常发生联想，如今的教师教学生，与挨了打的阿Q转而去摩挲小尼姑的光头，有没有本质的区别呢？<br />
屈辱和痛楚，就是要按照这样恶劣的方式复制感染下去吗？<br />
 <br />
授业是为了谁？为了学生的未来呢还是为了自己的生存？<br />
传道是为了谁?而且，当今之世，到底要传什么道才是道德的呢？<br />
解惑是为了谁？是做一个牧师神父乃至万能的主去解人之惑呢还是做一个无目无面无口无形的混沌混沌于混沌之间呢还是还另有千变万化的迷惑与千形万象的追索呢？</div>

							
							
							
							
