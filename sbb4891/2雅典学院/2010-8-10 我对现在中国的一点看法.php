													<h2>我对现在中国的一点看法</h2>
						
						
																											<div id="postmessage_777610" class="t_msgfont">人的幸福是非常短暂的，比起历史和政治，实在是牺牲不起的。但是在国家化大生产前提下创立的共产主义学说，因为过分强调宏观影响，要求集权，主张牺牲个人利益来完成国家整体的前进。中国从积贫积弱到国家整体实力跃居世界前列，用了三代人的时间。60年内、数十亿民众，抱着共产党许下的一个违背人性、飘渺的极乐世界主义幻想，默默忍受，教育、环保、文化、民主、医疗、创新都在推动国家整体前进的光环下暗弱无力，不是说共产党没有能力，而是共产主义制度就决定了主权高于人权；不是说我们的民众没有反抗，而是中国人的忍耐力极强，就是在被中共描述为地狱般的古代，百十年才有一次改朝换代。由这个制度所决定的，对人的忽视，对人性的忽视，对人权的忽视，导致对人所创造的思想文化的忽视，那么社会创新和社会勇气、文化推动力一步步走向衰竭，国家的未来在哪里？如今到了要变革的路口，共产党以“社会主义初级阶段”“稳定重要”“人口众多”来反对一切合理的质询！有一部分人说：”现在不是已经改革开放了吗？“是的，三十年来，国家整体实力按照预定计划进入世界前列，但更重要的是社会问题层出不穷，按住葫芦起来瓢，始终不能有效的解决，或者说根本解决不了，这正是在战时或是“乱世”时能显示强大力量的共产主义学说不能真正解决“治世”的问题，我们的教科书上还说过”像戊戌变法，改良主义，行不通！“不从根本上解决、不从制度上解决，社会迟早要崩溃的，文明迟早要衰亡的。<br />
&nbsp; &nbsp;&nbsp; &nbsp; 人，只能是目的，不能是手段。把国家这个手段凌驾于人这个目的之上，完全属于本末倒置。现在的国家是管理型的，用的是统治；而不是服务型的国家，用的不是奉献。从人的角度、从人权的角度，我们主张尊重每个人，继而是尊重民众、尊重社会，最后扩展到国家。应该是自下而上，民众自觉的推动社会，发展国家，而不是由上而下，笼统的以国家的整体实力为目标。先有人的福祉，再有国家的福祉，最终建成“小政府，大社会”的体制，即是活力社会带动服务型国家前进。<br />
&nbsp; &nbsp;&nbsp; &nbsp; 凡各国变法者，未闻有不流血而成功者！独夫民贼，已经不仅仅指的是某个人了。面对这个庞大的既得利益集体、面对这个空前凶猛的敌人，凡为党同志需做好被和谐肉身的牺牲准备。<br />
&nbsp; &nbsp;&nbsp; &nbsp; 那么，我们将以何种方式实现变法呢？<br />
&nbsp; &nbsp;&nbsp; &nbsp; 首先，我们不主张暴力，大规模的流血违背人权思想。<br />
&nbsp; &nbsp;&nbsp; &nbsp; 其次，共产主义学说不适合当今的中国国情。<br />
&nbsp; &nbsp;&nbsp; &nbsp; 再次，变法不能操之过急，需要分步骤实现。1.普及阶段：用一代人时间，把人道（保障基本人权）、人本（以人为目的的服务）、人文（尊重人的思想，鼓励创造，振兴文化）思想普及到每个中国人的心中。需要二十年时间。2.渐变阶段：普遍个人资本的壮大、社会矛盾的累积，要求利益再分配、权力再分配，由下而上的压力，敦促统治变革。需要二十年时间。3.准备阶段：中共内部寻求改变，开放党禁，建立稳定的民主党派。需要十年时间。4.实现阶段：建立服务型国家，三权分立，进行普选，彻底实现法治。需要十年时间。5.巩固阶段：致力民生，巩固民主，发展文化，形成大熔炉，兼容并包。需要十年时间。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 自此七十年内，中华之煌煌，逾千年之堑，亦将再立于世界矣。<br />
