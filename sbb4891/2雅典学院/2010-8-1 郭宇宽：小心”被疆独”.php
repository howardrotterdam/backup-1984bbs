													<h2>郭宇宽：小心”被疆独”</h2>
						
						
																											<div id="postmessage_768249" class="t_msgfont">郭宇宽：小心”被疆独”<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;1，<br />
<br />
这是我不可遏制的心智习惯,我喜欢与和我观点不一样的人交流，从中可以获得很多收获，如果一段时间，我讲的话，身边的人都说：“恩，有道理。”我就会感到一种难言的寂寞，甚至会上网找找有没有批评我的言论，最好有批评得尖锐些的。<br />
<br />
每到一个地方，我都爱寻找当地有意思的人交流，在我看了，最有意思的人，就是和我观点不一样的人。<br />
<br />
我讨厌文革和毛泽东路线，所以在印度我就特别去找在在丛林里打游击的毛分子。<br />
<br />
直到今天，我还有相当程度的文化意义上的中华情节，这个中华的概念甚至可以超出疆域的版图。所以在台湾我就特别去找“台独分子”。我原来不能理解一个也讲华语，和我们并没有有什么文化冲突的台湾人，会刻意强调自己不是中国人。当地朋友告诉我，林浊水是台独的精神领袖，是台独分子里最雄辩的，我就去找林浊水。<br />
<br />
我问林教父，你老家应该也是从福建来的，咱们的文化没什么隔阂，你的台独思想是从哪里来的？林浊水告诉我，他们这些台独分子，其实从小也都被教育自己是中国人，也觉得自己确实是中国人。<br />
<br />
但无奈，当年他们作为本省人缺乏政治参与的途径，当时的议员是一批49年从大陆来的“万年国代”。而且那时的蒋总统一心“反攻大陆，解放大陆同胞”，所以他们本省人提出一些要求民主的呼声，但凡牵涉到要照顾地方利益，和地方自治的，都会被斥为“台独分子”。<br />
<br />
“好吧，那我们索性就台独吧。”<br />
<br />
各式各样的台湾本省的地方诉求，最初是被贴上的台独的标签，后来这些人汇聚到一起，索性以台独作为旗帜，渐渐成了真台独。<br />
<br />
这个过程也可以叫做“被台独”。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;2，<br />
<br />
2003年，我刚从主持人转型为一线调查记者，做的第一个采访申请，就是要求去南疆，编辑部主任问我，你要去采访什么啊？我说，我也不知道，就是想找些和我不一样的人聊一聊。<br />
<br />
我先在在当地通过文学界和新闻界的友人，认识了一些当地维族朋友，我想我们不同的宗教和文化背景差异总会有一些不同的观点，我一路走到了靠近边境的南疆。没想到这些维族朋友知道我是一个记者的时候，他们的态度就像背稿子一样，讲得除了“我们坚决反对民族分裂”；“中央政策好”一类和新闻联播上一样，非常政治正确的话，接下来，除了谈谈新疆的天气和哈密瓜剩下来的就是沉默了。<br />
<br />
如果把这些政治正确的话，就当作新疆维族同胞的心声你就很难理解，为什么7.5骚乱中，你看内部录像，会看到那么强烈的无法掩饰的民族仇恨，一个汉族路人被打倒在地，一个穿着时髦的维族女孩，走过都会捡一块砖头，朝他头上砸，他们之间素不相识，唯一的理由是，他是汉族，我是维族。<br />
<br />
我不会为任何暴行辩解，但我能够一定程度上，比那些大人物更理解新疆维族长久以来的心情。是因为在我的努力下，我曾经听到了维族朋友，刺激我的心里话。<br />
<br />
在我和一个维族朋友真正建立了信任的关系之后，他知道我不是一个不负责任的记者，一次喝了一些脾酒，我们敞开心扉谈了谈，他对我说“我知道你不是一个坏人，我们是朋友，但是你们汉族人太坏了，太欺负我们维族了。”<br />
<br />
我说这该怎么讲，他说“你看你们中石油这些企业，在我们的家乡，圈起一块地，就抽油，把我们的环境都破坏了，用的都是安徽、河南的民工，不招我们当地的维族，油都送给北京上海，什么都不留给我们，看看你们搞石油的这些干部，开得都是好车，在最高档的酒店里吃饭的，吃完还开发票的，都是你们汉人。现在，在我们自己的家乡，我们维族是最穷的人，你看在便宜的小餐馆里，自费吃饭的都是我们维族人。“<br />
<br />
我说：“这不能讲是汉族人坏，这是我们国家的体制目前有问题，国有央企并不是单独对维族人的地方这样掠夺，它们对全中国人民的资源都是这样掠夺。国有企业和中国绝大多数老百姓都没有关系，其实就是国家干部所有企业，你不信可以上网查一查，对中石油中石化这些垄断的央企，汉族的老百姓也和你们一样骂得很凶，我相信这迟早会改革的”<br />
<br />
他大概觉得我在这点上至少讲得还有一些道理。他又举了另一个理由“那你们为什么要强迫我们学汉语，我们维族也有自己的文化和历史，现在在新疆不会汉语，就进不了政府，就找不到工作，这对我们非常不公平，你们汉人为什么不学维语？”<br />
<br />
我解释“语言的大一统确实有损文化多样性，这在中国是个体制性问题，49年以后把首都设在北京，所以北京话就成了标准化，全国人都得学说北京话。我会说比较标准的南京话，陕西话，还会说一些无锡话和上海话，但我也会说北京话，而土生土长的北京人就只会说北京话，而你要内地找工作，人家都要求会说普通话，没人要求会说陕西话，四川话，我想着主要是为了方便考虑。我如果有机会去新疆呆上两年，我肯定会认真学习维语的。”<br />
<br />
他找到了我的逻辑漏洞：“你说得不对，你那是说在内地，你在北京当然说北京话，在新疆的汉人，也很少学我们维语，你看那些外面派来的领导干部，在新疆待了十几年，除了一句亚克西，什么维语都不学。我们在自己的土地上，为什么要给当成二等公民？”<br />
<br />
我只能进一步解释，“其实很多汉族知识分子也抱怨，为什么现在所有的中国小孩都得学英语，英美人里学中文的就少得多，可这是一个世界潮流，我们也只能理解。我还到过克州，一个克尔克孜族的朋友，也跟我抱怨，‘那些维族人最坏了，他们人多，就欺负我们，我们克族人都得学维语，他们维族人从来不学我们克尔克孜语’。”<br />
<br />
这让那位维族朋友陷入了沉默，至少他原来以为理所当然而且情绪强烈的观点也是值得推敲了，后来他承认我讲得有些道理。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; 3，<br />
<br />
这些年来，我的经历让我越来越相信，除了有人根本就以仇恨为目的，否则不同的人群之间越多的机会能坦诚的说出自己的想法，哪怕是把负面情绪表达出来，越能达成同情的理解。人们越能全面的了解问题的本质，找出解决之道，越不容易导向诸如“汉族人坏”或是“维族人坏”这样简单而又极端的结论。<br />
<br />
2009年底，我又来到了乌鲁木齐，在骚乱之后，还没有恢复互联网和手机信息的新疆，一片压抑的氛围，我被一个当地朋友陪同走在二道桥，街上几乎除了我以外看不见一个汉人，我能很明显的感觉到一些不友善的眼神，而在维族餐厅里也看不到一个汉人，当地的汉族朋友说：“我们就不买维族人的东西，穷死他们”。我痛心民族的隔阂到了如此地步，而在骚乱后的这一年春节晚会上，唯一代表新疆的节目是穿着维族服装的歌舞“中央政策亚克西”。<br />
<br />
我这次又见到了几位维族的朋友，我想向他们了解，为什么事情发展到这样的地步，作为知识分子，我们是不是都应该做些什么？至少应该影响自己周围的人形成理性的态度，我们还是要在这片土地上一起生活，不要让民族仇恨发展下去。他们只是摇头，“我们现在什么都不能说”。我问为什么？他们告诉我，因为你是汉人，你对政府的政策提出意见，可以被理解成善意，而我们是维族，我们只能说“中央政策亚克西”，我们提出任何意见，别人都会说，你有民族情绪，你再说，别人就会说你是“疆独分子”。<br />
<br />
我当时很惊讶，现在看来这些维族友人的说法并不夸张，我刚看到消息，真正爱着这片土地和国家的维族中国人民共和国公民海来特.尼亚孜先生，于2010年7月23日，被新疆乌鲁木齐市当地法院以“危害国家安全罪”判15年徒刑。他的“罪证”，无非就是接受香港记者采访，公开谈出了自己对政府在75骚乱中处置不当的意见。我看了《亚洲周刊》记者李永峰的介绍，是海来特在七月五日上午十c，“面向新疆S吾自治^主席努．白克力提出三c建h的^。r他在朋友陪同下，本砣ヒ新疆h委王啡，但王啡十c之前不在豸木R，所以只好去努．白克力。他把正在h上的努．白克力了出恚面述了局莸呐Qc三c建h。努．白克力只是表示，要示上。按海R特的理解，就是f，他要示王啡。”最后的惨案，证明了海来特的正确判断和他不顾被误解的道德勇气、公民责任感和人道主义情怀。<br />
<br />
如果自治区热衷于维稳官员们，能够少一些官僚习气，能够多听一些海来特的建议，也许很多无辜的生命都可以被挽救。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;4，<br />
<br />
当一个民族的知识分子提出任何哪怕是理性的意见，换来的不是理性的回应而是被扣上“疆独”的帽子，这不是鼓励极端情绪么，这不是把人逼成疆独么？当一个公民，尤其是一个维族记者，仅是公开说了几句真话，就被当成“危害国家安全”，还能指望他的同胞来认同这个“国家”么？<br />
<br />
维族和汉族这样有着复杂历史纠葛的民族，如果真正能成为兄弟，唯一的出路，就是向南非那样组织类似“真相与和解委员会”的沟通平台。而不是压制真相和意见，来维持威权的稳定。<br />
<br />
在我看来那些要把海来特这样真诚表达自己意见的维族知识分子，是最宝贵的沟通桥梁，别人说他有民族情绪，就像我们汉族人也有民族情绪，但他至少认同这个国家，他至少尊重汉族并且愿意坦诚地和你对话。<br />
<br />
把这样的人抓起来判15年徒刑的势力，我相信是“一小撮人”为了自己的利益和脸面，做出的昏庸荒唐的事情。<br />
<br />
我猜想这“一小撮人”就是在新疆被称作“吃维稳饭”的一批人，他们不是化解，而是不惜给新疆的民族纠纷打上死结，新疆的民族矛盾越尖锐，他们越是能以此要挟中央政府，获得维稳经费，越是有铁饭碗。<br />
<br />
他们背叛了这个国家和人民，甚至也背叛了中央政府的根本利益，他们伤害了维族人，也不利于汉族人，这些人才是真正是“疆独”的同谋。<br />
<br />
作为一个汉族人，我第一用这个词称呼我的某些同胞，这些人是真正的“汉奸”。</div>

							
							
							
							
