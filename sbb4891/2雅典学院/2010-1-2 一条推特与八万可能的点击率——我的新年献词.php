																											<div id="postmessage_410666" class="t_msgfont">一条推特与八万可能的点击率<br />
――我的新年献词<br />
<br />
艾晓明<br />
<br />
新年有很多开始的方法，我的新年本来不必要这样开始的，由于一个小小的偶然性，警察敲开了一家人的门，把这家的主妇带走了；接着有人把电话打给我，通报了这个情况。我的2010，就这样慌慌忙忙地开张。<br />
<br />
警察在这新年一上班，辽阔祖国大地，就开演了这般阵仗；像我这样一个推特新手，都看到几条热闻，最搞的是我们广州的一位，那真是身手不凡，他能在警察破门之前，跳到了阳台上。继而就如王小波小说中唐朝的大军事家李靖一样，张开大拐，不见了。再后来，从台湾某个角落，有个自由什么亚洲的记者打来电话，问阿叔在哪里。你的阿叔，怎么来问我呢？我就这样，被拖入官兵捉强盗的大唐。<br />
<br />
时值九点，我本来是要做早餐的，我的早餐之重要，在于它是为我父亲准备的。我父亲新年高寿八十五，经历过战乱、当过历史和现实反革命，实属饱经沧桑。一年以前他不知从哪个亲戚带来的香港刊物上看到了零八县长，居然就记住了这是和我有关的一件事。并且，最烦人的是他还记住了刘晓波的名字，此后他又记住了刘晓波被离家出走，未知去向。有时冷不丁他就问上一句：你们那个刘晓波现在怎么样料呢？<br />
<br />
我老爹要是再问刘晓波，我就不好跟他扯野芝麻；我打算的是，首先要说到扫黄打黑的颠覆性含义、还有军事上以及艺术上的方阵列队花样……我是在编故事时接到阴险电话，并且，那一瞬间我的脑子进了开水：小谢被他们带走了，早上，两个警察来敲门。接下来，估计打电话的这个人也蒙了，等我的电话再打过去，根本没人接听。<br />
<br />
如果不是小谢，也就算了。神州大地的大尾巴狼很多，不缺我的斤两。但是小谢，这就没办法了，这个年没法过了。<br />
<br />
小谢就是谢怡卉，朋友你要是会翻墙，可以查到她的各种文字、摄影作品。我认识小谢，是因为去四川拍摄《我们的娃娃》，小谢的角色，假如我们是个大制片公司，她就等于是制片主任；可惜我们后台绵软、囊中羞涩，全靠一颗红心身兼数职。开始时我们一行四人，后来变为三个人，等到谭作人落网，我们就剩下俩，她还是个专业的失眠者，等于说劳动力只有一个半人。小谢开车、找吃找住找人出镜且拍照；我坐车、管问管录像并观望风向。我们要算很好的朋友，一路却也时有摩擦。小事包括午餐吃樟茶鸭还是猪耳朵，大事包括到底是旁观拍摄还是直接掺乎进去。说来你别不信，要是路上看见有人吵架，这个小谢是一定要去劝架的，我抢得好好的镜头画面，就这样穿梆漏水没法用了。<br />
<br />
不管我们吵过多少，这些都不干警察的事，大新年的你上门拉人进局子，叫人怎么想。还有，什么时候放、为什么？接下来还会发生什么？小谢家的先生和小花朵，下回我怎有脸见他们？从拍娃娃们开始，小谢陪我穿山越岭，那正是她女儿高考备战时分。几乎每天一见她，我就小心翼翼陪笑问：你老公今天不会追杀我吧？<br />
<br />
不用回忆，于私于公，我的犯罪记录历历在目。最新动作是《公民调查》，那结尾两分钟，用了小谢百多幅照片，全是摄于她和谭作人三千里川震校难调查之路。现在网上传的谭作人大头照，和小谢拍的没法比。我、谢怡卉、谭作人，我们三个人，不折不扣是一根绳子上的秋蚂蚱。但是比较起来，小谢又多了一条罪状，与艾晓明相互勾结，完成了《公民调查》。艾晓明的罪状还要更多，和刘晓波同署县长且拒不认错。既然小谢新年第一天进了局子，艾晓明没有理由逍遥法外，可能警察正在半路上，并且四川警方将和广东警方协同办案。<br />
<br />
在大唐的御林军开往红拂与李靖私奔的盘山小道之一千多年后，四川成都的志愿者小谢，坐在不知哪家的派出所里，回答御林军后代之讯问。小谢本不是个出格的人，就是因为她不出格，所以节假日老是闲不住。就像那歌里唱的，你总是心太软心太软，把所有问题自己扛。我问过她，谭作人进去了，你继续奔走在这条路上，你为什么不怕呢？小谢反问说：怕什么呢？她又说，二十几岁时，她看过一个电影，说的是一个人做志愿者，那时她就想，她以后也要做个志愿者。我们当时黑灯瞎火在夜路颠簸，仿佛爱丽丝漫游奇境，这个镜头我剪进了片子，不过没有用现场声音：人家做志愿者，做得好好的，咱们这个国度，就把自己做了进去。这就说明，不要看不正确的电影，导致人生走弯路。<br />
<br />
在我愁肠百转的同时，顺手在推特上发了一条消息：成都小谢，上午8点，警察带走，拜托狂推；狂的后面，特别加了个惊叹大号；这就说明我是菜鸟，人家专业一点都说RT的。<br />
<br />
这是本世纪2010年的第一天，离1970年的一打三反过了四十年。那一年要打的是“反革命破坏活动”，北京枪毙了遇罗克、南京枪毙了查金华，都是像谭作人、刘晓波这样的青年。四十年后的新年元旦，刘晓波要活活等过四千零二十一天才能回家；难道今天的现实比《天方夜谭》更魔幻吗？那个混世魔王苏丹被山鲁佐德的故事讲得心回意转，却也不过一千零一夜，难道听刘晓波故事的圣上，比一千年前的苏丹更愚钝？我又想到，谭作人蹲在看守所里眼看就一年，最危险的罪证，也不过是给王丹发了封电子邮件，王丹又不是苏丹，又有什么可怕呢？今天，我的合作者小谢，一个心生莲花的佛教徒、志愿者，大清早地给叫进了局子。这意味着什么？是那一打三反的运动又要渲染这个新世纪了吗？没盖过一间校舍的谭作人，要为孩子们的惨死蹲监狱；手无寸铁的刘晓波，能把堂堂中国颠了覆，这是空手套白狼还是马尾栓豆腐？<br />
<br />
在通往数盲和比傻的帝国，我与小谢的犯罪嫌疑密不可分；作为《老妈蹄花》的观众之一，不得不考虑必须展开的捞人行动：<br />
<br />
第一步：自制海报/状衣/头巾：《我们的娃娃》、《公民调查》――纪录片主创自首谢罪，恭请释放小谢。<br />
<br />
第二步：又不知道哪家派出所抓的人，并且，连小谢住哪条街也没记住，到成都后，只好直奔市局，设计对白：你是谁？艾晓明；哪个艾？艾微微的艾；有何贵干？要你们放人；她跟你什么关系？她是我的工作人员；你以为我是谁，难道人人都是徐科长吗？<br />
<br />
第三步：有关川震校难，还没有找我算帐，要是自投罗网，“当有想象空间”。从现在开始，应迅速开机记录：如何跟老爸交代刘晓波下场、如何买火车票上路、如何给律师写委托书。诸君，这可能是我校难三部曲的绝响啊：摄影、导演全玩完，还有比这更精彩的结局吗？<br />
<br />
在这三步展开之前，我已经看到：我的推特，跟进一千六；艾微微的推特，跟者一万六；崔老师的推特，跟者四千六，三下里加起来两万一千多。还有呐――冉匪匪的推特，跟者一万四，杨丽彩的推特，跟者两千一，燃烧弹嫌疑犯北疯，后面跟了一万三千五，小计两万九千六，两下合计过五万。我的个天，现在要说我的个推……大家一起推，上午10点半，我打了个电话给四川的朋友：小谢的老公跟我说啊，消息表上网；假如他跟我保持联系，我就忍了。但现在他失去下落，我要不上网，那我还是人吗？对方说，我们准备出发去小谢家；我说那你就跟她老公讲：大概有八万人，今天只要上推特，都会喊小谢回家吃午饭。对不起，如果过了晚上8点，可能就超过十八万了。<br />
<br />
在上午的12点差几分时，著名的匪仔冉发来一条消息，小谢同案陈云飞回家吃饭了。陈犯未犯拍片罪，我仍无法为小谢释然。小谢一分钟不自由，我脑子里的突发性事件继续上演：撕盖皮网友非撕不可推友自动组团，为《公民调查》续集现场拍摄，我眼前闪回黑光白光，西部列车沿线穿越一个接着一个的隧道，艾微微猫步伸展拾级而上；我想象找个替死鬼在成都市公安局的大楼前疾走，而我本人则潜身于几十个、几百个推友的摄像军团，在无数照相机、DV机、手机高举、光频闪动以及推特直播的时代，一场有关《公民调查》的公民调查，在2010年新年的成都，在烈女唐福珍殉难的天府心脏，盛大开场。<br />
<br />
所有这一切因为小谢中午回家戛然而止：人家就是被警察叫去问问情况嘛，你抓狂抓得满天下吊唁，把那个涉嫌造谣的公话快打爆了。<br />
<br />
这就是我的新年献词，今天，我享受了推特的神奇和快感。今天之后，我还有364天的推手操练，我将拥有的快乐和奇遇势必像初恋一样起伏跌宕。如果你像我一样心直口快且怀才不遇，那你就和我一起来推嘛。谦虚一点说，你可以不跟我，跟上AIWW、RANYUNFEI、WENYUNCHAO、SHAOHUACHEN、CUIWEIPING、YANGLICAI、LIANYUE等等，保证你不能升官发财，但一定有利于身心健康。活在这样一个充满风险和谋害的世界，还有什么比快乐和健康更重要呢？<br />
