													<h2>郭德纲事件的几个基本概念</h2>
						
						
																											<div id="postmessage_775682" class="t_msgfont">文章提交者：王来富 加帖在 凯迪西南 【凯迪网络】 <a href="http://www.kdnet.net" target="_blank">http://www.kdnet.net</a><br />
<br />
一、所谓“媒体”<br />
<br />
我们首先要明确，中国的所谓“媒体”跟其他地方的媒体有个本质区别，那就是中国的媒体是公权力的一个宣传机构，也就是说，它们本身就是公权力的一个部门。社会主义中国从来也不隐瞒这一点，说它是“喉舌”嘛，呵呵。而国外的媒体，大部门是民间的，即便是国家电视台之类，那也是全体纳税人的财产，它不代表公权力说话。<br />
<br />
在此事件中的另一方，即郭德纳，虽然他是公众人物，但他属于娱乐性的公众人物，那么这个矛盾就是官民矛盾，而不是像国外一样，媒体与娱乐性公众人物的矛盾是民与民的矛盾。<br />
<br />
明确了这点，那么我们就理所当然的要“屁股决定脑袋”，即我们应该本能地站在郭的一边，去监督另一方。比如，我们应该毫不犹豫地要求另一方拿出完整的录像，否则，全部错误都可以归于他们。而这种“屁股决定脑袋”，正是一个普通公民维护自己权利不受公权力侵害的唯一正确的做法。<br />
<br />
二、关于“私闯民宅”<br />
<br />
用更正式的说法，是“非法侵入私人住宅”。看过录像，俺觉得这个事实是完全成立的，至于是不是定“非法侵入私人住宅罪”，这要法庭来判决（因为要把某个行为定为犯罪，还要其他条件）。<br />
<br />
说这个事实成立，是因为当时记者有几个动作（小区保安已经提醒的情节，我们先忽略），即“敲门”、“推门”、“进入”。那么，当他们敲门后没人答应的情况下，就不该推门。什么叫 “非法侵入”，前提是“未经许可”，没人答应，当然是“未经许可”。让我们感到好笑的是，有人居然说没人答应，你怎么知道许可不许可呢？问题是，没人答应，就有主人不在家的可能。而只要有这种可能性存在，那你推门进入是啥意思？非偷即盗嘛，呵呵。<br />
<br />
所以，BTV作为公权力的一个机构，擅自推门进入他人住宅，那就是“非法侵入”了。假如换了一般人来王老头家，敲门无应，便擅自推门进入，俺完全就可以拿起菜刀让他滚出去。可正因为BTV觉得自己是公权力，才牛逼哄哄地这么做，我们当然要反对这种公权力漠视个人权利的行为！至于什么“事后默认”之类的浆糊说法，并不能否定BTV漠视公民权利在先的行为――而这正是我们要警惕和反对的东西！<br />
<br />
三、关于郭徒打人<br />
<br />
这个还不好判断是非，因为我们现在所看到的都是经过剪辑的公权一方的证据。这也是俺为啥一开始就要强调录像完整的道理。问题是，这种判断也应该由法庭作出，啥都不知道，就嚷嚷着“打人犯法”，那是标准的浆糊作法。王老头以为，作为普通公民，既权利也无义务去做这种审判，我们唯一能做的就是要求公权一方按公正的程序办事，不能任由他们把经过加工的证据作为判案的理由。<br />
<br />
退后一万步说，打人不对（这已经是浆糊的说法了），那郭徒也已经道歉，并被罚拘留了――这已经没啥可说的了。要说的应该是，这个判决，程序公正吗？！<br />
<br />
四、郭德纳被封杀<br />
<br />
这个好像没什么大分歧，比较一致的看法是封杀毫无道理，这就不多说的。<br />
<br />
&nbsp; &nbsp;需要说的是有些糊涂看法。比如律师小h就认为郭那句“打就打了呗”不对，问题是，对不对你可以讨论，但不能拿这个作为封杀郭的理由。一个娱乐性的公众人物说啥话，只要他不怕失去观众或听众，又关别人屁事啊！小h好像说这对孩子会形成不好的暴力影响，可这个责任不在郭，而在公众自己，让孩子看什么不看什么，是你家大人的责任，也可以说是公众自己的责任。或者你可以按影片审定的方法，把郭的相声定为“三级相声”，少儿不宜嘛。无论如何，这不是扼杀郭发表言论的理由，娱乐性公众人物，除非他自己愿意，他有啥义务要做社会的道德楷模？！你自己把他当成道德楷模，那是你自己的事。<br />
<br />
就说这些，欢迎探讨！</div>

							
							
							
							
