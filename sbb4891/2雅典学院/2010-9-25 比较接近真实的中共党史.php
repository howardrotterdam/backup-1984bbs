													<h2>比较接近真实的中共党史</h2>
						
						
																											<div id="postmessage_843477" class="t_msgfont">首先我决定从历史讲起，这段历史从中国共产党成立讲起。<br />
 <br />
&nbsp; &nbsp;&nbsp; &nbsp; 按照官方的说法，中国共产党成立由陈独秀创立，并于1921年召开了一大，于是一个伟大的政党诞生了。那么比较真实的情况是怎么样的呢？当时陈独秀是北大的校董之一，但是生性浪荡，有一次嫖娼，不信消息走漏，北大的其他校董觉得有伤风化，于是召开了民主会议，将陈独秀开除了北大，百无聊赖的陈独秀，在郁闷中碰到了共产国际派来的某位同志。此同志的具体名字本人愚钝记不清了，共产国际就是苏联。这位肩负共产主义使命的同志告诉陈独秀，可以在中国也成立一个共产党，如果有可能，那么，就带领被压迫的人民建立无产阶级政权。于是，在1921年的7月23号，几个教师、记者，还有几个无业游民，一起开了一个会，于是宣称一个伟大的政党成立了，若干年后，它自称带领中华民族推翻了三座大山，是国家和民族的代表。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;先分析这段历史，我们应该用怎么样的角度去看呢？当时，国民政府已经建立，那么当时的中国正统是中华民国，撇开军阀中可能还有不听话的，最起码当时名义上是一个统一的国家。而共产国际的同志到中国来对陈独秀说，你可以成立一个政党来推翻现在得政府，那么，这个行为是否可以理解为一个国外的反动势力，在中国阴谋策反？如此一来，那么我们伟大的党从它成立的刹那起，便是一个由国外的反动势力为了在中国阴谋侧反而扶植起来的傀儡。而事实上，当时苏联如此做的原因，本意是想在东方扶植一个完全听它控制的国家，我们的外蒙古也因为这样才被独立了出去，另外我们不知道的是，在共和国建立之前，新疆就曾经独立过两次，原因全都是苏联想在东方扶植几个完全听它控制的国家。苏联本质上是一个万恶的组织，下面会继续介绍。<br />
刚刚我提到，几个教师、记者，还有几个无业游民，一起开了一个会，于是宣告了一个伟大的政党成立，是想突出与会成员身份的混乱与低贱，而并不是传说中什么伟大的时刻。比较真实的说法是应该是几个无聊又混得不太好的人，一起坐下来喝了喝茶，并由陈独秀牵头，说我们成立一个中国共产党，我们是第一批党员，于是在座的人纷纷表示同意，起草了一份文件，于是中共就这样成立了。<br />
 <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;接下来是第一次国共合作，当时孙中山尚为国民党领袖，据说，在国共两党一起努力下，加速了中国革命的进程，在中国革命历史上出现了轰轰烈烈的大革命。但是到了大革命后期，1927年7月，由于蒋介石和汪精卫控制的国民党右派不顾以宋庆龄为代表的国民党左派的坚决反对，宣布与共产党决裂，发动了“四一二”、“七一五”反革命政变，公开叛变革命，致使第一次国共合作破裂。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;前面提到过，中国共产党从它成立开始，就是由邪恶的苏联预谋在中国策反而成立的组织，在这期间，共产国际时不时地希望提醒中共，要发动工人运动，要夺取政权。不想，没有不透风的墙，消息走漏，蒋知道后自然十分生气，于是杀掉了一些能抓到的中共党员。在我党的大力宣传下，这次事件变成了蒋介石蓄意破坏两党合作，破坏中国革命的反革命事件。不过试想，对于当时的正统国民政府来说，有人预谋推翻自己的政权，那难道不是很严重的叛乱？应该杀掉。<br />
 <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;于是老毛到了井冈山，开始了他的星星之火实践。这里我们得换一种思路，我们被灌输的历史唯物主义导致我们很难认清历史的真相，在教科书中自吹中共是历史的选择，因为其他几个阶级的革命都失败了，具体的不说了，参见教科书，急性好的再回忆回忆。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;就算我们不坚持这种观点，但是这也已经在我们的潜意识中留下了根深蒂固的观念，认为国民党剿杀共产党就是逆历史潮流的反动行为。但是，如果我们站在当时的国民政府角度来说，有一个组织预谋推翻自己的政权，不把这个组织剿灭那才是非常奇怪的行为。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;接下来发生了我们党一直吹嘘着的四次反围剿和最后一次因为里德博古的战术失误才导致的失败。也许事实可以是这样的：为什么把围剿分成五次?事实上可能就是一次，只不过派了五次兵而已，这导致围剿的人数越来越多，使得井冈山的中共军队越来越难以招架。为什么说前四次采取了毛主席同志正确的战术？即：在游击战中集中优势兵力，各个击破。这两个疑问比较真实的解答是这样的：自围剿开始之日起，中央红军预计到自己的实力与国军相差太远，于是采取了逃跑的战术，一直利用自己熟悉当地地形的优势与国军躲猫猫，在躲避的过程中，偶尔发生了点小冲突。所以说，游击战可以理解为逃跑战术。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;那么最后为什么会失败？那是因为蒋公派来围剿的人数太多，而井冈山就那么一块地，显然已经没有足够的空间继续玩躲猫猫游戏，于是只能硬拼，于是失败了，于是逃跑了，当然自称是长征。当然有人就得为此被黑锅，于是拉来了里德博古凑人数。<br />
 <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;于是长征开始了，我军四渡赤水，翻雪山，过草地，飞夺泸定桥，历经千难万险，来到陕北会师，接着北上抗日。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;79年，邓小平在访问美国的时候，在回答记者提问时主动承认：飞夺泸定桥啊，哈哈，根本没那回事，那是为了故意宣言我们战士英勇无畏而编造出来的，当时桥对面根本就没人，我们不慌不忙地走了过去而已。至于那雪山，当时由于季节原因，上山根本没有雪，只是因为海拔高，有几个战士有点高原反应。<br />
长征就是一场彻头彻尾的逃跑，没有想象中玄乎得在世界战争史中都堪称奇迹如此这般的伟大。<br />
 <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;日本侵华后，全国人民在我党带领下积极抗日，而蒋是消极抗日，我党又发动群众，广泛建立敌后根据地，后来又有了平型关大捷，百团大战，带领全国人民经过8年抗战，赶走了日本侵略者，挽救了民族。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;对于这段历史，我们有没有怀疑过，就凭这么几个大捷大战就能赶走了日本人？而且根据惯例，教科书中的内容一定是经过苦苦深挖，会把所有我党光辉伟大的四季都写上去，但是就算是这样，似乎我们也只看到了有一个平型关大捷，还有一个百团大战，还有吗？那么我们是不是该怀疑一下日本为什么撤走？而这时国军在干什么？难道什么事都没有做？因为书没有说蒋介石打过什么仗，只是给他定性为消极抗日。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;建国以后，毛泽东在见到一位日本访者的时候，亲口叙说：没有你们皇军侵略大半个中国，中国人民就不能团结起来对付蒋介石，中国共产党就夺取不了政权！<br />
&nbsp; &nbsp;&nbsp; &nbsp; 而根据史料，毛泽东在当时的情况下，认为这是千载难逢翻身机会，于是订下纲领：一分抗日，二分宣传，七分发展。于是，在正面战场国军与日军拼死拼活的时候，我们伟大的党在敌后小打小闹了，在控制的区域内打地主分田地来发展自己，说起来就似乎变成了日本人会投降唯一的原因就是我党的英明作为。打地主可以争取光大人民的信任，人多就是力量啊，至于打到地主是不是人性化的，非暴力的，统统不管。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;打胜了百团大战的彭德怀却在党内被指责暴露了党的实力，背上了罪名，而这个罪名也伴随了他一生，直到在文革中被整死。至于死因，还有更加内幕的解释，暂且不提。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;至于建国后那些表现抗日战争中敌后战争的影视作品，比如地道战地雷战，还有什么铁道游击队等等，清醒地想想，就凭挖挖地道，埋几颗地雷，炸掉几条铁路，就能赶走日本人？那都是善于搞宣传的中共宣传部弄出来的，而为什么中共如此看重宣传，因为当年老毛就是靠宣传起家。为什么宣传如此重要，因为宣传具有洗脑的作用，第一次国共合作的时候，年轻的老毛就在国民党内拼命搞宣传，造成了恶略影响，这也为第一次国共合作失败埋下了伏笔。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;举个例子，神乎其神的地道战，真实的情况是怎么样的呢？是因为红军十分惧怕日本士兵，因为日本士兵的战斗力举世罕见，罕见到什么程度？为什么为有如此强大的战斗力，原因很复杂，可以再开个专题来分析了，暂且不提。因为惧怕，所以，红军总会在村庄周围埋些地雷来作为预警信号，地雷一爆炸，红军就马上逃跑。但是毕竟日军来进攻次数还是少的，所以大多数时候乡亲们踩到地雷的概率比较大，因为地雷的位置不会告诉乡亲们的，怕有奸细出卖情报。所以，传统的军民互爱联合抗日的形象，应该改为红军不顾老百姓死活，老百姓对红军憎恨这样一种关系。而事实上，传统说教中，红军专门到被地主被鬼子占领的地盘去帮助老百姓进行解放，也并非是事实，事实是，红军只到没有敌人的村庄去，已有敌人来就跑。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;一方是八年的消耗，一方是八年的发展，玉蚌相争渔翁得利，如此完美地演绎了这段历史。<br />
 <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;于是我们党以逸待劳，一鼓作气把国民政府赶到了台湾，在那里，国民政府延续了它98载的建国岁月，并将继续延续下去。<br />
 <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;建国后不久就发生了抗美援朝战争，美帝国主义妄图侵略朝鲜，并将战火蔓延了到了鸭绿江边境，为了保卫新生的共和国政权，同时支援朝鲜人民的反帝斗争，中国派出志愿军进行了抗美援朝战争。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;但是真实的情况是怎么样的呢？当时北朝鲜的金日成妄图统一朝鲜半岛，于是向南韩进攻，同时向苏联和中国要求支援，苏联考虑到自己的过于偏北的国土没有一个不冻港，于是同意支援金日成的侵略战争，但是自己又不想出兵，于是要求毛泽东出兵朝鲜，当时中共政权刚刚建立，对苏联依赖很大，于是同意出兵支援朝鲜，注意了，这是在支援一场侵略战争，而非传说中的美国侵略朝鲜，中共在帮助反抗侵略战争。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;战争的结果是双方谁也没有多占便宜，于是不了了之，于是签订了停战协议，朝鲜半岛的内战也就这样丑陋地结束了。而大陆和台湾的内战呢？至今双方都没有签订过停战协议，所以法理上将我们依然处于内战时期。从另一个角度讲，感谢美国，替南朝鲜保留了一块民主制度统治下的土地。<br />
 <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;知青下乡的真实原因。当年老毛提出让知识青年到农村去，接受贫下中农的再改造。这是多么勉强的理由，真正的原因只有他自己心里最清楚，由于当时实行的仍然是严格的计划体制，而这种拙劣的制度已经被证明是极端没有效率的，这些知青在毕业后都是得由国家统一分配安排的，于是这种僵化的体制在面对越来越对的干部身份毕业生时，越来越感觉到力不从心。于是无奈的老毛叹息道：让他们下乡，自生自灭去吧！<br />
 <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;对于89年的天安门事件，有多少人知道有这么一件事情，又有多少人知道真相，但是可以肯定的是，我们伟大的党，代表人民的党，最终是开着坦克包围了天安门广场，倾轧游行的学生，机关枪扫射无辜的人民。而这些人，无非是要求民主，要求李鹏下台，要求纪念胡耀邦，同时继续进行反腐败斗争。我们的党在那时就已经自我证明了权贵阶级已经在10年的改革开放中获得了垄断利益，他们不想放弃这些特权，而且打下江山的那一代领导人也尚在人世，不肯拱手将自己一手打下的江山让人。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;这次事件导致了二三年中国经济的停顿，全世界都在观察，中国会走回文革时代，或者是一个更加开放的时代，于是，在92年邓小平南巡讲话后，中国未来的走势顿时明了，于是外资又开始蜂拥而入，国内投资也重新恢复。所以，中国真正的开放应该得从92年开始计算，但是就在那时起，中国也奠定了权贵资本主义的社会特色。全球第二的税率，但是全球第一的法国是把财政收入的42%用于人民的福利，而中国只有10%。<br />
