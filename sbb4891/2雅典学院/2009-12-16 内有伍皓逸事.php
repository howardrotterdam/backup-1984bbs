																											<div id="postmessage_376058" class="t_msgfont">未名湖连中南海――江泽民总书记与北大学生座谈前后<br />
 <br />
古健　周玲&nbsp;&nbsp;(1990.04.14)<br />
<br />
　　未名湖连中南海<br />
&nbsp; &nbsp; ――江泽民总书记与北大学生座谈前后<br />
<br />
　　古健　周玲<br />
<br />
&nbsp; &nbsp; 北京大学给江泽民同志写信的１１位同学，作为总书记的客人走进了中南海。普通学生进中南海与总书记座谈，这在北大历史上还是第一次！<br />
<br />
&nbsp; &nbsp; 这几位同学怎么想起给总书记写信的？座谈的详情如何？从中南海归来，他们作何感想？<br />
<br />
&nbsp; &nbsp; 在首都大学生中，传递着这样的问讯。<br />
<br />
&nbsp; &nbsp; 信的由来――<br />
<br />
&nbsp; &nbsp; “写出大学生正在思考的问题和希望”<br />
<br />
&nbsp; &nbsp; 在一个细雨绵绵的日子，记者来到北京大学，寻访给江泽民总书记写信的同学。<br />
<br />
&nbsp; &nbsp; 哲学系的曹天亮、黄绍举、冯刚，法律系的刘剑平，国际政治系的潘成鑫，七嘴八舌地介绍了写这封信的原委。<br />
<br />
&nbsp; &nbsp; 那信是一次座谈会的产物，是北大学生热情与困惑的心声。<br />
<br />
&nbsp; &nbsp; １９９０年１月中旬，校刊邀请了十几名同学举行题为“迎接九十年代”的座谈会，参加座谈的同学大都互不相识，但共同的话题使他们很快熟悉起来。他们从学校的思想政治工作谈到时代精神，从民族气节谈到自己的爱国之情、报国之志，其间也谈到党和国家领导人最近多次去基层接触群众，特别是江泽民同志先后去过北京第二外国语学院、清华大学、西安交通大学，还给北京医科大学的布依巴雅尔同学写过回信。大家感到，党中央新的领导核心带头发扬党的优良传统，联系群众，国家大有希望，９０年代大有可为。同时，他们也谈起社会上对大学生、尤其是对北大学生的一些看法；有的单位对去参加社会实践的北大学生不大欢迎；毕业分配去基层，有些同学怕得不到合理使用。这些都使他们感到困惑。向谁来倾吐自己的心声呢？谁能给以热情的引导呢？一位同学提议，我们能不能也给总书记写封信？与会者一致赞同，推举中文系的伍皓、哲学系的曹天亮分头起草。<br />
<br />
&nbsp; &nbsp; 伍皓文思泉涌，一下子写了５０００言；曹天亮则难以提炼自己的思想，觉得写上５万言也难把心中的话说完。<br />
<br />
&nbsp; &nbsp; 寒假很快到了，两位起草者的脑子里，还在琢磨着这封信，春节过得也没有像往年那样轻松。<br />
<br />
&nbsp; &nbsp; 开学了，冯刚、黄绍举、刘剑平也参加信稿的修改。<br />
<br />
&nbsp; &nbsp; ２月１５日深夜，４个人终于改出一封１２００字的信稿。刘剑平回忆说，当时就是要说说我们这一代学生正在思考的问题，希望中央领导同志作为我们的师长和朋友，有机会多和同学们交谈。曹天亮说：我们这封信就是为了同党中央沟通。<br />
<br />
&nbsp; &nbsp; ２月１６日晚，１１位学生坐在一起，通过了写给总书记的信。该签名了，谁第一个签呢？有人提议，就按现在坐的位置依次签吧。但马上被否决了。曹天亮想，曹字笔画多，就建议按姓氏笔划多少来签。“这样签意义也不大”，又有人反对。这时，潘成鑫建议：“咱们给党的总书记写信，还是由党员同学带头签名好。”大家一致赞同。３名党员、８名团员中，首先由共产党员、法律系８６级达斡尔族学生金黎明签了名。<br />
<br />
&nbsp; &nbsp; 载着１１位同学的希望，信在第二天就转送到了中共中央办公厅。<br />
<br />
&nbsp; &nbsp; ５天后，国家教委副主任滕藤、北京市委副书记汪家H来到北大，告诉写信的同学们：江泽民同志收到了你们的信，让我们来和同学们商量，你们愿意让总书记回信，还是希望同他面谈？同学们喜出望外：“我们当然是希望和总书记座谈了。”<br />
<br />
&nbsp; &nbsp; 如沐春风――<br />
<br />
&nbsp; &nbsp; 充满着关怀、信任和期望的四个小时<br />
<br />
&nbsp; &nbsp; １９９０年３月２３日下午２时３０分，１１位写信的学生，还有从石家庄赶来的两位正 在那里参加军政训练的学生，和校党委正副书记王学珍、林炎志一起，坐车来到怀仁堂。<br />
<br />
&nbsp; &nbsp; 此后的情景，像刀刻一样留在了同学们的记忆中。<br />
<br />
&nbsp; &nbsp; 潘成鑫：江泽民同志一落座就说，本想给大家写封回信，但书不尽意，已经动笔又停了下来，觉得还是和大家见面谈一次为好。根据中国礼仪，你们先谈，然后我再讲。希望大家坦诚相待，说心里话。“人非圣贤，孰能无过”，讲错了没有关系，请大家畅所欲言。一席话，顿时使我们的心与总书记贴近了许多。　<br />
<br />
&nbsp; &nbsp; 冯刚：总书记进来后和大家一一握手。王学珍同志又向他一一作了介绍。介绍到我时，党委书记说我翻译出版了一本小册子《优哉游哉》。总书记感兴趣地问我，优哉游哉英语怎么讲啊？我想不起来了。我说，这个书名是意译的，原书名是《人生的艺术》。总书记听了马上说道，噢，Ａｒｔ　ｏｆ　ｌｉｆｅ。我立即回答，ｙｅｓ，ｙｅｓ，大家发出一阵会心的笑。这笑声给座谈带来了亲切、愉快的气氛，把我们心中那一丝紧张感冲得无影无踪。<br />
<br />
&nbsp; &nbsp; 李湖生（地质系８７级）：我们参加座谈的同学都是普通的学生，尤其像我这样，并不是学生干部，能在中南海与总书记座谈，心里很激动。特别是总书记在谈到青年知识分子成长道路时，讲述了他自己从上海交大电机系毕业后，向一个宁波籍师傅学习修电机的事，让人感到很亲切。总书记讲完自己的经历，还向左边转过脸问李铁映同志，你在基层搞了多少年科研？铁映同志回答：２０年。总书记又向右边转过脸问李锡铭同志，你也在电厂干过多年吧？李锡铭回答：干了２６年。总书记接着提到，大学毕业后先到基层去锻炼，和工农大众相结合，前途是无量的。从组织上来说，对到基层工作的同学们要尽量合理使用，从同学们来说，应该把个人利益和国家的、民族的需要结合起来。此时此刻，听到他讲的这些道理，真是受教育，受感动。中央领导同志本身就是这样走过来的，他们对待我们确实是诚心诚意的关怀，寄托着殷切的希望，决不是作个样子给人看的。<br />
<br />
&nbsp; &nbsp; 总书记循循善诱，对同学们提到的问题都作了解答。尤其使大家难忘的，是当谈到对青年的评价时，他说，世界是你们的，也是我们的，但归根到底是你们的。毛主席的话现在也没有变。你们当中最年轻的１９岁，最大的才２３岁，毕业后还能工作近４０年。你们是跨世纪的一代，实现社会主义现代化第三个战略步骤的责任落在你们身上，要勇敢地担当这一任务。当然，我们对青年既要满腔热情，又要严格要求，没有严格不是爱护。同学们一边听一边思索。要担当历史的重任，难道不应当严格要求自己吗？要为祖国战胜前进道路上的困难，首先就得有勇气面对眼前的一些不顺利的情况。冯刚说，我当时心里真是热乎乎的，真正感到自己对国家负有责任。作为大学生，我们要严肃地对待人生，对待学业，对待生活。不应当为了个人而生存，要为社会、为国家、为人民、为理想而奋斗。<br />
<br />
&nbsp; &nbsp; 谁也没想到时间过得那么快，座谈共进行了４小时１６分。总书记还向同学们说，也没有来得及准备便餐，很对不起。大家告诉总书记，别担心，学校食堂给留着饭呢！<br />
<br />
&nbsp; &nbsp; 学生社团“社会主义研究会”的会长黄绍举，向总书记汇报了研究会的活动。临走时，总书记握着他的手说，高等学校有积极因素，要发挥积极分子的作用，使更多的人学习和掌握马克思主义的理论。我相信，在九十年代，从年轻的大学生中必定会成长起一批马克思主义者。黄绍举回忆当时的心情，真切地感受到了党对青年学生充满着信任，也坚定了自己对党的领导的信心。<br />
<br />
&nbsp; &nbsp; 眼看就要告别亲爱的师长和朋友，同学们恋恋不舍。大家又提出了合影和签名的要求，总书记也愉快地答应了。<br />
<br />
&nbsp; &nbsp; 春风化雨――<br />
<br />
&nbsp; &nbsp; 北大学生在思索，北京大学在思索<br />
<br />
&nbsp; &nbsp; ３月２４日清晨，电台播送了江泽民总书记邀请北大学生到中南海座谈的消息。同日，首都各大报纸也都作了报道。许多师生兴奋地向参加座谈的同学打听详情。<br />
<br />
&nbsp; &nbsp; 黄绍举座谈归来，与冯刚、曹天亮一起向全系的教师和同学作了一次介绍。虽然只讲了２０多分钟，但会场的热烈气氛，使他们感觉到老师和同学们是愿意尽快听到党中央的声音，希望与中央保持一致的。不时有人插话，要他们多讲点。黄绍举说，那种感受是多少年来所没有过的。在研究会会员中，他也汇报了与总书记座谈的内容。本来他想当晚马上汇报，因为借不到教室，只得推迟一天。为了完成总书记的嘱托，北大社会主义研究会近日请求老师开列了一个研究课题系列，再围绕课题开出了书目，开始进行系统的学习。这个研究会如今已由发起时的７人增加到５０多人，吴树青校长应聘为顾问，校党委副书记林炎志是名誉会长，一些研究马列主义和社会主义的教授，像张岱年、梁柱、黄楠森等，担任了他们的顾问。黄绍举说，我们一定要站稳脚跟，搞好学习，吸引更多的同学参加。<br />
<br />
&nbsp; &nbsp; 北大党委非常重视这次座谈。３月２７日，分别向党委常委会和学校民主党派负责人传达了江泽民同志的谈话精神。<br />
<br />
&nbsp; &nbsp; ４月２日，学校又召集中层以上干部，传达贯彻江总书记的讲话精神。<br />
<br />
&nbsp; &nbsp; 一些５０、６０年代从大学毕业的老师，似乎格外关心这次座谈。同学们说，可能是中央领导同志的行动引起了他们对当年的回忆吧。那个年代，老一辈革命家经常接触大学生。老师们当学生的时候，大都听过周总理、小平同志、陈毅同志的报告。<br />
<br />
&nbsp; &nbsp; 也有的老师说，去年的政治风波后，外界对北大的议论很多。现在党的总书记邀请北大学生进中南海座谈，看来中央还是很关心北大的。当他们听说江泽民同志谆谆告诫同学们要继承和发扬北大革命、进步的传统时，感慨地说，这几年出现的无政府主义、个人主义、民族虚无主义，决不是北大的传统。北大应该加强管理，严格校风，特别是我们教师，为人师表，一定要尽心尽力，引导同学们健康成长，成为社会主义事业的合格接班人。<br />
<br />
&nbsp; &nbsp; 看到参加过座谈的同学们兴奋的面孔，我们也为他们那种“如沐春风”的心情所感染。这次座谈，有如春风化雨，渗入了一个个青年学生的心田。他们对记者说，总书记和我们谈心，也是和北大，和全国的青年学生交心，这对增加中央领导同志和青年学生的沟通，增强我们民族的凝聚力，是非常有益的。如今，我们在思索，整个北大在思索，相信北大会有一个更光荣的明天。</div>

							
							
							
							
