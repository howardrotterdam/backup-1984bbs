													<h2>温氏困局</h2>
						
						
																											<div id="postmessage_865676" class="t_msgfont">现在回头去看，从今年元旦开始，中国总理温家宝就开始他的“突围”之旅了。<br />
<br />
元旦祝词中，他说要让人民有尊严的生活，之后在两会报告中强调政治体制改革的重要性和紧迫性，5月在《人民日报》上撰文深情缅怀老上司胡耀邦的亲民作风，8月在深圳特区30年纪念大会上，明确地讲：没有政治体制改革的保障，经济体制改革的成果就会得而复失，现代化建设的目标就不可能实现。这次在接受CNN专访时，谈及政治体制改革，甚至说出了“风雨不倒，至死方休”这样令人惊愕的话来，比当年的朱F基“地雷阵”和“棺材”说，有过之无不及。<br />
<br />
在中国，一号人物是总书记而非总理，大政方针、改革方略也是由总书记而非总理出面昭告天下的。现在，总书记不表态，总理似乎越位，多次指明一个国家的政改方向，这的确会让外界展开各种联想。依我看来，原因大概有以下两个：<br />
<br />
其一，温家宝的个人价值观是倾向民主和法治的，这从他就任总理来一系列的讲话就可窥出一斑。我不同意某些人冠之“影帝”的说法，因为政治人物不同于普通人，有时他的“说”比“做”更重要，有时“说”其实就是“做”。我们得承认在这个世界上，精神有高下之分，言论同样有轻重之别。所谓“人微言轻”，这无论在民主社会还是专制国度，都差不了多少。只是在民主社会里，经由制度（民主选举和独立媒体）的设计，普罗大众可以集合他们的“微音”而变得“响亮”，使得掌权者不得不俯首倾听。但显然，当下的中国并无这样的制度安排。<br />
<br />
其二，温家宝身陷困局，需借此“越位”之举进行“突围”。这两年居京城听来四面八方的消息称，温家宝处境艰难，“倒温”的传言就起过几回。想来也不奇怪，他虽然贵为一国总理，却实际上是一个大管家，按通行说法是CEO，而他手下的几位财经部委首长却是上任总理的人马，且任免之权不操他手，自然共事起来不会太默契，甚至“阳奉阴违”恐怕也不可避免了。<br />
<br />
国资委前主任李荣融卸任时提到的是前总理朱F基而非温家宝，称朱“是我学习的榜样”。当然，人事还不是问题的关键所在。利益才是决定一切的根本。当年朱F基搞国企改革，让一大批国企破产工人下岗；搞政府职能改革，让一些人手中权力缩水。这些都触及到了被改革对象的切身利益，遭到强烈的反弹可想而知，因此他才会近乎宣誓地说“鞠躬尽瘁，死而后已”。<br />
<br />
如今，温家宝面临的困境比朱更甚。改革到了深水区，曾经绕过的障碍摆在了面前，不能再回避了。只有一条路：不前进，就倒退。要缩小已超过警戒线的贫富差距，改革收入分配机制，必得触动既得利益者的利益，这次不是无力的下岗职工或人微言轻的普通公务员，这次是有钱有势的权贵。他们在朝在野经营多年，彼此勾连，互相支持，形成了一股非常强大的势力，在某种程度上可以“挟持”政府。<br />
<br />
即便是总理，在如此强大势力面前，也显得势单力薄。从目前情形看，温家宝似未获得高层同僚的有力支持，有点“孤军奋战”的意味，且处于交锋的下风。于此困境中，他不惜“越位”，大声疾呼政治体制改革，期望获得体制外的声援，以“民意”来增加他的“战斗力”，改变不利的处境。<br />
<br />
近来一批知识分子，其中不乏京城知名人士如杜导正、崔卫平等人发表文章或接受访谈，力挺温家宝。胡耀邦之子胡德平也站出来，澄清此前网上有关胡家人不满温家宝的传言，对其肯定有加。最近《时代》亚洲版以他做封面，也算是一种外援吧！<br />
<br />
当然，所谓“倒温”的传言，也离不开权力争斗的大背景。随着十八大的临近，各派势力抢位战日趋激烈，使出各种手段，其中也包括“制造不利于对手的舆论”。<br />
<br />
不过，“温”是不会“倒掉”的。胡耀邦和赵紫阳的前例不会发生在温家宝身上，理由很简单：世上再无邓小平。但我对温在余下的任期不抱太大期待，他虽不至于“被倒掉”，作为估计很有限了。<br />
<br />
最后说一句：我以为，贬温家宝为“影帝”，实在对他有些不公。在目前形势下，他“说的多做的少”是正常的，况且他“说的”都是令人向往的词汇。相比之下，他的同事们“说的”大多令人郁闷并生气。</div>

							
							
							
							
