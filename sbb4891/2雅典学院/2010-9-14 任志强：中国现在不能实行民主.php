													<h2>任志强：中国现在不能实行民主</h2>
						
						
																											<div id="postmessage_829911" class="t_msgfont">批评政府是“忠诚性反对”<br />
<br />
Q：您在微博和博客上对政府的房地产政策有比较多的批评和意见，但那天潘石屹说，其实您是一名坚定的布尔什维克。为什么您还要对政府的政策有这么多批评呢？<br />
<br />
任志强：因为我希望它好啊，不批评不是希望它变成更坏吗？所以有一种反对叫“忠诚性反对”。中国(历史上)的大部分反对是“非忠诚性反对”，就是要把你推翻了我来当家作主，从陈胜吴广开始一直到现在为止都是要推翻你，包括朱元璋、洪秀全、李自成什么的，要推翻你我来当(统治者)。国外不是，你看英国两党打了这么长时间，都没有推翻英国女皇；日本这么打都没有推翻天皇，美国这么打都不会推翻《宪法》，这就叫“忠诚性反对”。不管两党怎么斗，可以打得你死我活，但最后它会以国家利益为主，而不是非要把你打倒了我来上，这就是差别巨大的地方，共产党打国民党就是我要来当家作主。<br />
<br />
Q：但中国没有两党，您是老党员了。<br />
<br />
任志强：中国没有两党，但你要把共产党推翻了让别人上台那更不行了，让别人上台还不如共产党上台，这不是很清楚的事吗？我们的十几个(民主)党派算起来，哪个党派能有七千万人口？不可能，没有这么大的力量去保护中国，你也不可能形成有效指挥。萨达姆被推翻以后，伊拉克群龙无首，变得一团混乱，阿富汗也同样，所有被外来政权解放的国家都是群龙无首，因为没有办法统一。所有外来给你的民主都是失败的，起码到目前为止美国扶持的总统没有一个是能统一的，我们希望共产党改的更好，如果共产党能修改自己的政策，一定会比现在更好。邓小平时代为什么人心没有出现问题？就是改革开放显示了共产党好的一面，文化大革命显示了共产党差的一面，或者是指挥、政策出现错误的一面。如果改革开放政策能够坚持下去，这个国家会越来越好，所有人都会是改革开放的受益者。<br />
<br />
因为是亲儿子所以敢说<br />
<br />
Q：您是很敢说话的，也是比较乐于说话的。别的房地产商，比如潘石屹、王石和冯仑似乎也很愿意说。<br />
<br />
任志强：他们不敢说。<br />
<br />
Q：没您敢说？为什么？<br />
<br />
任志强：因为他们是私有企业，我是国有企业，你愿意让我当我就当，不愿意让我当就拉倒，我们叫做“一纸官文”，任命你就是你，不任命你就不是你，干部调动都这样。所以我无所谓。<br />
<br />
Q：您无所谓？<br />
<br />
任志强：布尔什维克，掉脑袋都不怕，还怕这个？他们小资产阶级就怕，这不明摆着吗？王石说我是什么？我是亲儿子，共产党员、劳动模范、国有企业；他是私生子，既不是党员又不是劳动模范，既不是人大代表又不是政协委员，当然不如我敢说。<br />
<br />
Q：潘石屹呢？他有时候也敢打擦边球。<br />
<br />
任志强：他是人大代表，有法律保护，但他们还是以生意为主，我不是以生意为主，因为挣的钱不是我的，国有企业挣的钱不是我的，是国家的；他们挣的钱放自己兜里了，我们挣的钱装在国家兜里。<br />
<br />
中国现在不能实行民主<br />
<br />
Q：您心目中理想的中国政治、经济制度应该是什么样子的？<br />
<br />
任志强：中国政治可能会从党内民主慢慢形成到国家民主，但在现有条件下不能实行民主，我认为不具备民主的条件，你到农村去看，大部分民主都是黑社会，用钱、用利益来获取，用利益获取我并不反对，但要把村子的利益改善得更好。但现在不是这样，他谋取自己利益，比如给村里捐一条路，却把马路管理权交给自己家族里的人了，去收过路费，建设权垄断、管理权垄断，农村有这种现象。<br />
<br />
所以现在我们的情况是，民主是从最不懂民主的地方开始民主的，而最应该民主的地方却没有民主。党内民主完全可能，应该说最有学问、最有知识的人都在这7000万人里。(如果)这7000万人里有好几个派，就像日本，一个自民党里有好几个派，不一定会形成颠覆。<br />
<br />
Q：会互相制约。<br />
<br />
任志强：党内民主不是要变成另外一个党，不会这样。但现在我们的选票是没用的，是党在指定候选人，党不尊重人民的权利，这个市长是人民选出来的，最后他随随便便就给调走了，人民选的你怎么能随随便便调走？调走之前你得征求一下人民的意见啊，你说调走就调走？你又揽一个候选人过来，我没选他啊。<br />
<br />
Q：我们多数情况下不知道这个人是谁(笑)。<br />
<br />
任志强：这是一个有待解决的问题，这不等于不能解决，但现在要坚持一党制，否则搞民主的经济成本太大。用国民党的说法就是在人均GDP达到6000美元至12000 美元的时候我们才能实行民主，之所以这样就是因为人民的生活不会因为民主颠来颠去而变得贫苦，虽然经济会下滑，但他不会贫困，会撑得住。美国有民主是因为它撑得住，阿富汗那么穷，民主了半天他们也上不去，没用啊。<br />
<br />
政府自利时也会有公利<br />
<br />
Q：您觉得经济体制应该是什么样的？<br />
<br />
任志强：现在分蛋糕的过程是个利益博弈的问题，但自利的时候也会有公利，否则把蛋糕分完了它就倒了。道理是一样的，党为了自利时也得弄点儿公利，不弄点儿公利，没有发展，它分得也越来越少，这两者在一个阶段里还是同步向前的。<br />
<br />
你看现在的中国，虽然很多人很保守，虽然很多人很自利，但他不会忘记公利，比如地方政府要用土地财政来赚钱，但它不会把土地财政全贪污了，还是会用在桥上、用在路上、用在公共交通上，还是城市的人在享受。现在这些基本是在用高房价撑着，比如北京地铁2块钱，如果没有高房价做补贴，2块钱怎么可能撑得住？所以不能说地方政府把所有的钱全用在坏处上了，还是很多用来修了桥、修了路，还是有改善，他在自利的过程中给了一部分公利出去，要不然他怎么能当官呢？有人批评城市修广场不好，但修广场最后不是大家享受吗？<br />
<br />
Q：我就批评修广场。因为把老的广场拆掉，然后盖新的广场，我觉得这是浪费。<br />
<br />
任志强：有些地方政府是有浪费，谁让他这一届干不完就走了呢？我这一届总要做点事儿。退一步说，如果没有当时的皇帝修故宫，今天能有故宫吗？就永远不会有，所以当时皇帝的奢侈和腐败还是给你留了点儿东西的，要不这个还留不下。<br />
<br />
Q：对皇帝这个说法我有保留意见。<br />
<br />
任志强：我觉得没有对和错的问题，只是当时时代的需要，不能总用后人的眼光来看前人的，现在的批评大部分都是事后诸葛亮，用后人的眼光来看前人。<br />
<br />
微博比博客好<br />
<br />
Q：您在微博上非常活跃，粉丝数量飞速增长。您花得时间很多啊，我数了一下，8月5号那一天就发了61条。您觉得微博对您有改变吗？<br />
<br />
任志强：对我没什么改变，对老百姓有改变，很多人管它叫“自媒体”，博客和微博都是自媒体，我写博客是因为媒体老歪曲我。<br />
<br />
Q：您现在新浪微博的粉丝有多少？<br />
<br />
任志强：163 万，我和潘石屹还差一万(编者注：截至9月12日，任志强新浪微博粉丝接近180万)。潘石屹90万的时候鼓励我上微博，一开始我上博客也是他怂恿的，他说老被媒体歪曲，你得上博客自己说。上博客自己说就是不要让媒体歪曲，他说了不算，以我写的为主。微博呢，潘石屹跟我说，博客上有很多人天天骂你(他老找人删，我从来不删)，“微博上没多少人骂你，微博比博客好”，于是我就开始上微博了。<br />
<br />
Q：还是要花些时间的。<br />
<br />
任志强：要维护它，要让粉丝满意，要不断回复。实际上我的微博也占用很多时间，但我不是专门为微博而微博的。<br />
<br />
Q：是利用中间的空隙？<br />
<br />
任志强：比如在机场等飞机，要不看书要不微博，微博和看书也一样。比如晚上看了一个纪录片《公司的力量》，我就边看边发微博，它不是专门占用你的时间。其实可能你还在干别的事儿，点两下，写两句，看看，回复一下，其实回复也就是几个字。单说时间，确实占用了一部分时间，不管怎么样它是一个事儿。<br />
<br />
Q：我觉得您在微博上更放松了。<br />
<br />
任志强：其实原来我也很放松。<br />
<br />
Q：但不知道为什么很多时候的语气让人觉得您在骂人，或者是在生气。<br />
<br />
任志强：只是很多人没有接触过我，了解得少。原来没接触过，被媒体妖魔化了，所以脑子里有一个妖魔化的印象，觉得我是坏人，接触完了不也挺好？<br />
<br />
Q：也说不上是坏人。<br />
<br />
任志强：反正不是好人。<br />
<br />
潘石屹是遵守契约精神的朋友<br />
<br />
Q：您和潘石屹(在微博上)挺好玩的。你们什么关系？<br />
<br />
任志强：我们观点不一样，但生意上建立的关系可以互相信任，我说他什么没关系，他说我什么也没关系，无所谓，因为我们做了四五次生意，生意都很大，第一次就是他在万通。<br />
<br />
Q：那是很早了。<br />
<br />
任志强：1993 年的时候，他根本就不懂。第二次生意，我们俩只用了一张烟纸盒。他大半夜跑去西苑饭店，拿个烟盒纸写上几条，然后签个字，就交给我了。后来一笔40亿的交易，也是没纸，在昆仑饭店，我还要去机场，他非要找我谈半个小时，我们俩就在昆仑饭店的菜单上写，写完了成交。到今天为止他也认为跟我签合同是最保险的，没有什么乱七八糟的事。同样，只要我们在合同上签了字，契约精神建立了，不管谁吃亏了都认帐，这是他之所以信任我的原因。按道理说，竞争对手形成这样的信任不太容易，而我们处理得很好。<br />
<br />
因此朋友间不在于你和他有多少私人关系或者怎么样，完全不是，而是契约精神，做过一件事情后你一辈子都信任他，虽然我们在其它方面的观点可能不一样，但还是有信任的，他跟我做什么生意他都相信我，即使我骗了他他也认帐。<br />
<br />
Q：您会骗他吗？<br />
<br />
任志强：我不会。<br />
<br />
Q：我也觉得是。<br />
<br />
任志强：这就是遵守契约精神，事先没想好的你活该，但说了就算。<br />
<br />
