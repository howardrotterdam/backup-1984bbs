													<h2>司马南：共产党员大于基督徒</h2>
						
						
																											<div id="postmessage_198513" class="t_msgfont">中国基督徒超过共产党？<br />
――南方周末声言“基督教已经获得普世性”引发的思考<br />
司马南<br />
<br />
在2009年7月28日出版的《纽约时报》上，《南方周末》再一次受到关注和表彰。起因很简单，7月10日的《南方周末》发表了一篇题目为“基督教在中国迅速崛起”的文章，文章披露部分美国人对中国基督徒数量的一种估计――约7000万之众――与中国共产党员的总数（7593.1万名）大体持平。<br />
<br />
《南方周末》作为中国大陆纸媒公开披露这个消息，很合乎《纽约时报》的胃口，很吻合帝国的“政治正确性标准”，于是，《纽约时报》默契地和着《南方周末》的节拍“互动”起来。<br />
<br />
《纽约时报》终于在《南方周末》上“看到了他们想要看到，并且可以引述来源的事实”：“基督教在中国有着显著的成长” “如今基督教席卷中国”，且这种增长“引起中国官方不少部门、学者以及主流媒体的注意与探讨”。<br />
<br />
一条消息足够好，会令人产生新的兴奋点，使人暂时忘却其他烦恼。《纽约时报》本来兴奋点在新疆，本来正在借乌鲁木齐7・5事件日夜辛苦地抹黑中国，看到这个消息，忽然满脸堆笑，像一个拿到更好玩儿的玩具的小男孩，痴痴地冲着《南方周末》笑起来。<br />
<br />
小男孩尊称其为“中国一著名的报纸”，接下来不厌其烦地介绍其历史与出身：“出版于中国广州，面向全国发行，发行量达600多万份”――看看，不光华尔街金融家的数字靠不住，美国媒体所报的数字更不靠谱――他们将《南方周末》发行量放大6倍后向全世界传播，可能会让这张报纸显得比实际发行量更著名更重要一些。<br />
<br />
中国媒体宣传据说有个“潜规则”，即对内对外有两套发行数字，但是，即使最不要脸最善于吹牛的媒体，也不大好意思把对外发行量放大到“写意吹牛”的水平。这是一个很有趣的例子，读者可以借此案例来研究一下，动机，究竟是怎样影响知觉的？美国媒体，为什么会满嘴跑火车信口雌黄？《南方周末》，为什么在他们看来有那么大的发行量？《南方周末》为什么那么着《纽约时报》宠爱？<br />
<br />
《纽约时报》称：《南方周末》“是属于全国著名品牌报章之一。与其它国内报纸相比，它更显开放，报导一些其它媒体很少报导的事件或现象，读者多为知识分子，也曾多次撰写不少与基督教相关话题，来引导人们思考基督教在中国的发展”。这一段话，基本没有说谎，它扼要概述了《南方周末》对基督教在中国传播所做的特殊贡献，以此作为“《南方周末》感动美国的颁奖词”大体不错。<br />
<br />
《南方周末》刊登的文章是这样分析“基督教之所以开始在中国兴旺起来”的原因的：一，“官场腐败”；二，“环境污染加剧”；三，“中国制造在全球形象受损”；四，来自于基督教本身――它“为中国民众提供了一种道德框架。”<br />
<br />
文章作者伊万&#8231;奥斯诺斯将中央政治局的集体学习制度与基督教在中国发展胡乱挂钩断章取义：“Hu Jintao主席“去年亲自主持政治局宗教‘学习班’，向中国最有势力的25位领导人表示，必须团结信教群众的智慧和力量建设繁荣社会。”<br />
<br />
《南方周末》转载这篇文章时，特别加了“点评者说”却对文章的错误观点未加任何修正。在《南方周末》看来，美国人宣传的“基督教给中国人提供道德框架”的说法是事实，“中国制造在全球形象受损”也是事实。而Hu Jintao去年主持政治局宗教学习班的核心内容――“拥护中共”、“拥护社会主义制度”、“维护祖国统一”等被完全阉割掉了，“依法管教”、“自主办教”、“独立办教”等关键内容也不见了踪影。<br />
<br />
“本文的数据未知可信否。<br />
<br />
不过信上帝,总比迷信金钱或者权力好”<br />
<br />
――《南方周末》的全部点评，只有这轻佻滑头的两句话。<br />
<br />
就是说，对“中国有7000万基督徒”这件事，《南方周末》白纸黑字张扬得天下人尽知，却试图凭这一句话而不准备负任何责任――反正已经传播出去了，倘有人叫真儿，尽可一推六二五了事。《南方周末》甚至会自我辩解：我说7000万基督徒并不多呀。<br />
<br />
没错，《南方周末》的辩解不是完全没有道理。同期《纽约时报》引述香港某报文章《他们信奉神(In God They Trust)》，数组更加离谱更加耸人听闻：中国各地的基督教教徒正迅速增加，最近已经超过共产党员总数……天主教和各派基督教信徒加在一起超过1.25亿。<br />
<br />
请大家留意国内外媒体互动的规律――对一则并没有得到确证的消息，对传播开来可能对中国有负面影响的消息，对独家刊发可能会引发麻烦的消息，他们近一两年通常是这样玩的：1，《纽约时报》说，“中国有7000万基督徒”，消息源是中国报纸《南方周末》；2，《南方周末》说，“中国有7000万基督徒”，消息源是《芝加哥论坛报》；3，《芝加哥论坛报》说“中国有7000万基督徒”，无需告诉你消息源是哪里，你管不着！（中国国内的读者根本也看不着）就信口胡编了说你了，你能怎么着？――引朋呼友，相互印证，彼此遮掩，随意缩放，叠加震荡。<br />
<br />
当然，《纽约时报》不会仅仅满足于披露中国基督徒的数字，他们更善于从历史背景的分析中，揭示出《南方周末》之所以需要表扬的理由。《纽约时报》笑眯眯地讲起《南方周末》去年4月份干的一件好事。旅美华人学者吴澧在《南方周末》刊发文章，题为“基督教徒70%在第三世界”。文章着意批评中国人：不少中国人仍然认为基督教是西方宗教，其实“基督教在今天，更是第三世界的宗教。”“宗教已走向全球化，基督教已获得了普世性……”<br />
<br />
