																											<div id="postmessage_198544" class="t_msgfont">中国到底有多少基督徒？<br />
<br />
何当<br />
<br />
从（1908年）马礼逊踏上澳门码头那一天算起，两百年后的今天，中国到底有多少基督徒？<br />
<br />
这似乎是一个人人都知道答案的问题，尽管大家都宣称，无人能知道这个问题的真正答案。国内官方宣称只有1400万（最新数据称有1600万），但是大多数华人基督徒的看法远比这乐观。一位北美的弟兄在文章里这样说：“海外一些研究人员估计，1996年，中国大陆基督徒的人数已达5000万至<br />
8000万之众。”前不久，一个基督徒常去的论坛做问卷调查，多数参与者都相信，今天中国大陆基督徒人数已经超过8000万，甚至可能超过一亿。<br />
<br />
8000万，这也是我信主以来一直接受，并且一直很受鼓舞的数字：每人只要传一个福音，把自己的老婆，父母，同事什么的弄进教会，就是1亿6000万啊！如此说来，中国的福音化不是很快就要实现了么？<br />
<br />
但现在，我对这个数字的信念发生了动摇，这个改变并非刚刚开始，但却是在编辑《教会》第二期的过程中逐渐清晰起来的。按照戴文涛弟兄在《皖北教会二十年兴衰路》中描述他们教会的说法：<br />
<br />
“人数最多的时候是95、96年，有好几百个聚会点，很多点都上百人，多的就有二百人。而现在，到2005年，陈建弟兄（主要带领人）下面带的不到二百间教会，有的几十个人，有的一、二十个人，最多的点也不过百。甚至陈弟兄自己家里的聚会都停止了。”（25页）<br />
<br />
从这一个案来看，该教会在90年代中期，最兴旺的时候，大概有信徒3万到5万人，而到2005年（也就是十年之后），则只有约5000到15000人，人数至少下降了一半，最严重估计，则可能只有原来的十分之一！<br />
<br />
而且，这剩下的不到一半信徒，其人员组成也发生了很大的变化。戴弟兄说：<br />
<br />
“现在我们教会，就像我这么大年纪的，几乎是没有了，全都上外面打工去了。陈建的孩子做生意，又是主要同工，又是带领聚会的，散了会就去做生意，到礼拜天才回来。”（28页）<br />
<br />
也就是说，不但是人数在明显减少，该教会中青年信徒的比例也更是急剧下降，到了几乎没有壮劳力或者说中生代的地步。<br />
<br />
这间教会的情况是特例么？就我所知道的，似乎不是。韩泉水弟兄是一位来自河南的传道人，他在本期《教会》所发表的文章《河南教会问题再思》中也提到了类似的问题：<br />
<br />
“教会中问题重重，聚会人数稀少，有时同工也不聚会，大家听道昏昏沉沉，心不在焉。我的几位亲戚是三十年前的老信徒，现在连读经、祷告都不参加，主日礼拜也不参加。”（17页）<br />
<br />
“因为教会对于工人供养工作的消极，很多有能力的工人因生活所迫，就放下手中禾场的工作，出去打工，以解决生活的难处。羊群因无人牧养，灵性下滑，生命长期处于饥渴状态。”（18页）<br />
<br />
这两篇文章的内容，虽然没有更广泛、更精确的统计数据作为佐证，但是仍然十分具体地描画了中国农村教会现在荒凉而危险的局面，更不用说江登兴弟兄在他那篇非常沉痛，非常有意义的评论文章《中国教会的专制主义危机》中所揭示的令人触目惊心的问题了。我相信，所有真正关心中国教会发展的人如果读了这一期的《教会》，可能就会警觉到，中国教会现在所面临的，恐怕绝对不是什么暂时的发展低谷这样的小问题。<br />
<br />
回到开始的问题：今天中国到底有多少基督徒？大家普遍认为，中国家庭教会的人数要数倍于官方教会，在我们所相信的8000万这个数据中，如果<br />
1400万是官方教会，那么，还有大约超过6000万就是家庭教会的信徒。我相信这个数字应该包含很大的水分。曾经有知情人告诉我，河南某大团队，对外宣称有1000万信徒，实际上不过50万，只是所宣称的二十分之一！这中间令人瞠目结舌的差距，既有十年间人员的流失，显然也有浮夸虚报的问题。<br />
<br />
即使我们采用6000万这个数据，如果我们假设，在过去十年间，农村教会聚会人数平均减少了一半（而不是上面这些个案所反映的，十分之一甚至二十分之一），为3000万，加上官方教会到今天是1600万，则今天中国还在教会聚会的基督徒只有4600万，这是往多里算，要是往少了算，多少就不知道了，并且，其中老弱病残又占了很大的比例。所以，不是超过8000万，不是超过一亿，我可能是保守的看法，中国现有基督徒的人数是4000万。<br />
<br />
当然，中国教会在过去十年中也并非全是衰落。沿海一些地区，北京、上海这样的大城市在2000年之后的发展非常的显著，教会很兴旺，人数显著增加。一些教会受到海外机构和城市新兴教会的帮助，也在健康地发展着，我们应该把这一部分增长算进去。<br />
<br />
但是，城市人口毕竟是相对少数，像北京这样情况特殊的城市并不太多，更何况，我们有什么理由肯定，城市教会一定不会重蹈农村教会的覆辙呢？至少，如果《中国教会的专制主义危机》一文所揭示的问题在城市教会中也存在的话，我们就不能对未来抱盲目乐观的态度！<br />
<br />
我突然发现，对于中国教会有多少人这个话题，我已经不再津津乐道了。8000万，这个我和很多弟兄姊妹都挂在口头的数字，到底有多少实在的意义呢？如果教会没有真正归属并高举基督，没有成为真正神的教会，不是8000万，而是别的什么更大的数字，有什么本质的不同呢？在现在这个阶段，数字不应该成为我们的目标，我们的意义。在中国教会属灵的争战和行军过程中，比起数点民数，我们有很多其它相当重要的事情要做。</div>

							
							
							
							
