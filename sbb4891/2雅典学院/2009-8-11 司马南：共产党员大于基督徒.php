													<h2>《Economist》去年10月2号的报道</h2>
						
						
																											<div id="postmessage_198889" class="t_msgfont">Christianity in China <br />
<br />
<a href="http://www.economist.com/world/asia/displayStory.cfm?source=hptextfeature&amp;story_id=12342509" target="_blank">http://www.economist.com/world/a ... p;story_id=12342509</a> <br />
<br />
Sons of heaven <br />
Oct 2nd 2008 | BEIJING AND SHANGHAI<br />
From The Economist print edition <br />
<br />
Inside China’s fastest-growing non-governmental organisation<br />
<br />
EPAJesus loves you<br />
ZHAO XIAO, a former Communist Party official and convert to Christianity, smiles over a cup of tea and says he thinks there are up to 130m Christians in China. This is far larger than previous estimates. The government says there are 21m (16m Protestants, 5m Catholics). Unofficial figures, such as one given by the Centre for the Study of Global Christianity in Massachusetts, put the number at about 70m. But Mr Zhao is not alone in his reckoning. A study of China by the Pew Forum on Religion and Public Life, an American think-tank, says indirect survey evidence suggests many unaffiliated Christians are not in the official figures. And according to China Aid Association (CAA), a Texas-based lobby group, the director of the government body which supervises all religions in China said privately that the figure was indeed as much as 130m in early 2008. <br />
<br />
If so, it would mean China contains more Christians than Communists (party membership is 74m) and there may be more active Christians in China than in any other country. In 1949, when the Communists took power, less than 1% of the population had been baptised, most of them Catholics. Now the largest, fastest-growing number of Christians belong to Protestant “house churches”. <br />
<br />
In a suburb of Shanghai, off Haining Road, neighbours peer warily across the hallway as visitors file into a living room, bringing the number to 25, the maximum gathering allowed by law without official permission. Inside, young urban professionals sit on sofas and folding chairs. A young woman in a Che Guevara T-shirt blesses the group and a man projects material downloaded from the internet from his laptop onto the wall. Heads turn towards the display and sing along: “Glory, Glory Glory; Holy, Holy, Holy; God is near to each one of us.” It is Sunday morning, and worship is beginning in one of thousands of house churches across China. <br />
<br />
House churches are small congregations who meet privately―usually in apartments―to worship away from the gaze of the Communist Party. In the 1950s, the Catholic and main Protestant churches were turned into branches of the religious-affairs administration. House churches have an unclear status, neither banned nor fully approved of. As long as they avoid neighbourly confrontation and keep their congregations below a certain size (usually about 25), the Protestant ones are mostly tolerated, grudgingly. Catholic ones are kept under closer scrutiny, reflecting China’s tense relationship with the Vatican. <br />
<br />
Private meetings in the houses of the faithful were features of the early Christian church, then seeking to escape Roman imperial persecution. Paradoxically, the need to keep congregations small helped spread the faith. That happens in China now. The party, worried about the spread of a rival ideology, faces a difficult choice: by keeping house churches small, it ensures that no one church is large enough to threaten the local party chief. But the price is that the number of churches is increasing. <br />
<br />
The church in Shanghai is barely two years old but already has two offspring, one for workers in a multinational company, the other for migrant labourers. As well as spreading the Word, the proliferation of churches provides a measure of defence against intimidation. One pastor told the Far Eastern Economic Review last year that if the head of one house church was arrested, “the congregation would just split up and might break into five, six or even ten new house churches.” <br />
<br />
Abundant church-creation is a blessing and a curse for the house-church movement, too. The smiling Mr Zhao says finance is no problem. “We don’t have salaries to pay or churches to build.” But “management quality” is hard to maintain. Churches can get hold of Bibles or download hymn books from the internet. They cannot so easily find experienced pastors. “In China”, says one, “the two-year-old Christian teaches the one-year-old.” <br />
<br />
Because most Protestant house churches are non-denominational (that is, not affiliated with Lutherans, Methodists and so on), they have no fixed liturgy or tradition. Their services are like Bible-study classes. This puts a heavy burden on the pastor. One of the Shanghai congregation who has visited a lot of house churches sighs with relief that “this pastor knows what he is talking about.” <br />
<br />
Still, the teething troubles of the church are minor compared with the vast rise in the number of Christians. After the Tiananmen Square massacre of 1989 many disenchanted democrats turned to Christianity: six of the 30 or so student leaders of the protests became Christians. China’s new house churches have the zeal of converts: many members bring their families and co-workers. One Confucian Chinese says with a rueful smile that most of the pretty girls at university were ChristiansCand would date only other Christians. <br />
<br />
Holier and trendier than thou<br />
Christianity also follows Chinese migration. Many Christians studied in America, converted there and brought their new faith home. Several of the congregation of the Shanghai house church studied abroad, as did Mr Zhao. In 2000, says one Beijing writer and convert, most believers were in the countryside. After 2000 they brought their faith into the cities, spreading Christianity among intellectuals. <br />
<br />
All this amounts to something that Europeans, at least, may find surprising. In much of Christianity’s former heartland, religion is associated with tradition and ritual. In China, it is associated with modernity, business and science. “We are first-generation Christians and first-generation businessmen,” says one house-church pastor. In a widely debated article in 2006, Mr Zhao wrote that “the market economy discourages idleness. [But] it cannot discourage people from lying or causing harm. A strong faith discourages dishonesty and injury.” Christianity and the market economy, in his view, go hand in hand. <br />
<br />
So far, Christianity’s spread has been largely a private matter for individual believers. The big question is whether it can remain private. The extent of its growth and the number of its adherents would suggest not. But at the moment, both Christians and Communists seem willing to let a certain ambiguity linger a while longer. <br />
<br />
“Christians are willing to stay within the system,” says Mr Zhao. “Christianity is also the basis for good citizenship in China.” Most Christians say that theirs is not a political organisation and they are not seeking to challenge the party. But they also say clashes with public policy are inevitable: no Christian, one argues, should accept the one-child policy, for example. <br />
<br />
Formally, the Communist Party forbids members to hold a religious belief, and the churches say they suffer official harassment. The president of the Beijing house-church alliance, Zhang Mingxuan, was thrown out of the capital before the Olympic games and told he was unwelcome when he returned. In early June, the state government of Henan arrested half a dozen house-church members on charges of illegally sending charitable donations to Sichuan earthquake victims. CAA claims harassment of house churches is rising. <br />
<br />
In fact, the state’s attitude seems ambivalent. In December 2007, President Hu Jintao held a meeting with religious leaders and told them that “the knowledge of religious people must be harnessed to build a prosperous society.” The truth is that Christians and Communists are circling each other warily. But it is hard to avoid the conclusion that Christianity will have a political impact one day. “If you want to know what China will be like in the future,” concludes Mr Zhao, “you have to consider the future of Christianity in China.”</div>

							
							
							
							
