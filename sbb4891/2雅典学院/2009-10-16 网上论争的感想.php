我有一个观点，假如你在写作中很高调，办事一定要低调。为什么？因为文章多了，本身就会出现审美疲劳，或者审丑疲劳。你再尽情表演，好日子就要到头了。说得高尚一些，我认为，作家的光荣是他提供的作品，而不是到处张扬。喜欢在大庭广众下表演的人，不论是作家还是演员，都不会有好作品了。<br />
<br />
在网上，我比低调还要低调。在那个网站，还有其他地方，主要是人少的地方，我写信给版主，不要把我的文章置顶。<br />
<br />
就是这样，问题还是发生了。在辩论中，甚至还不算辩论，只能够算讨论，突然有人说：你不要太看重其他论坛的恭维，那都是很表面的。朋友的批评是真诚的，朋友的肯定才是最有价值的。<br />
<br />
有人以为特立独行，其实和别人一样，陷入了俗套。在这里，也有人说：“我看您是在其他地方当教主，当精神领袖当习惯了吧”。为了不断章取义，我说明，这是针对我说是“被请来”的话。我就是被请来的，这是事实。有人说这仅仅是几千分之一，那没有关系。<br />
<br />
我想，其中的思维方式，不过是：<br />
<br />
第一，刘松萝在外面有一些影响，不能让他太张扬；第二，虽然他现在不张扬，有可能是假的，要防患于未然。也许还有第三，你在外面风光，就要让你在这里碰碰钉子。<br />
<br />
归结起来，就是改造知识分子，使其夹起尾巴来。<br />
<br />
即使是斗士，也少不了江湖的习惯。先探探虚实，再找找弱点，适当出击一下，发现技止此耳，最后一举拿下。《黔之驴》重现了。<br />
<br />
最不可思议的是，有人还自称这是为了朋友好。我想问，这个朋友并没有张扬，不需要杀威棒和下马威，你把他的自信打下去怎么办？这是朋友所为吗？<br />
<br />
假如说，你在别处受欢迎，不一定说明水平高，这是说得通的。但是，以后逻辑在事实上变成：你在别处受欢迎，正说明你水平低。我们知道，水平高和受欢迎有时是正相关的。按照正常的逻辑，刁难受欢迎的人，是不是你的水平低啊？<br />
<br />
其实，还是不自信的表现，是自卑心理在作祟。不过有人要记住，低调并不是软弱，不可以欺软怕硬。那么多论坛，哪里不可以去？<br />
<br />
人与人之间是不平等的。假如讲不平等，假如讲江湖的道理，那么也是有一些成绩的人在上面，而不是那些捣乱者。<br />
<br />
这是一个类似于猴山的世界。我看到，那些惯于撒泼打滚的人，却没有人劝他们低调了，反而变成率真的真名士。众多自以为新潮的人，跟着他们去做乏味的游戏。<br />
<br />
这是一个什么世道！人心啊，人心！</div>

							
							
							
							
