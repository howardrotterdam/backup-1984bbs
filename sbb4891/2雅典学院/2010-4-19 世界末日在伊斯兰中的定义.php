													<h2>世界末日在伊斯兰中的定义</h2>
						
						
																											<div id="postmessage_571809" class="t_msgfont">无论你是不是穆斯林，都进来看看吧，这片文章是我从校内里一个穆斯林那拿过来的，希望能让更多人看到...）<br />
奉至仁至慈的真主之名！<br />
&nbsp; &nbsp; 最近，看到很多人在校内状态里写到对《2012》的看法，<br />
有的说：“特别震撼，那场景真是无法想象，一部好电影”；<br />
有的说：“太恐怖了，要是真的发生了怎么办...”；<br />
当然，还有人说：“都是假的，根本不可能发生的事情...”。<br />
因此，我特地查了一些有关资料后写出了这些，希望与你们一起分享。<br />
<br />
&nbsp; &nbsp; 首先，对于世界末日的来临， 《古兰经》有详细的描述： <br />
&nbsp; &nbsp; 「其时天空裂开，海洋汹涌，群山摆动，太阳被遮蔽，世界因而变得黑暗，人类将会经历前所未有的恐惧，无人可以逃避或藏匿。 」<br />
&nbsp; &nbsp; 「大难，大难是什么？你怎能知道大难是什么？在那日 ，众人将似分散的飞蛾，山岳将似疏松的采绒。至於善功的份量较重者，将在满意的生活中；至於善功的份量较轻者，他的归宿是深坑。你怎能知道深坑里有什么？有烈火。 」（古兰经第101章）<br />
&nbsp; &nbsp; 万物非主，唯有真主，独一无偶，国权归他掌握，赞颂全属于他，他使人活，叫人死，他全能于万事!作为穆斯林，我们坚信真主的存在，相信真主的经典，当然也相信世界末日的来临。<br />
<br />
&nbsp; &nbsp; 举个简单的例子吧，在同一个教室里听课的一帮学生，老师是同一个人，并指出了哪个是该学的哪个是不该学的。而学生是自愿的，有的学习很优秀，接受那些知识；而有的总是逃课，平时也不学习。最终，期末考试到了，学习好的自然取得好的成绩，讨老师喜欢，将来也会有个美好的未来；而不学习的呢？自然会挂课，在学校受老师的冷漠，在家受到父母的批评。<br />
&nbsp; &nbsp; 生活也是如此，真主明确告诉我们哪个对哪个错，并且给了我们思考的能力（这是人类与动物的唯一的区别），同时给了我们充分的自由，在今世完全任由我们自己做出决定。有的人意识到了这一点，为了得到真主的喜悦，他尽可能的多做好事，多做善功；还有的人否认真主，到处做坏事，损人损己。（在这儿不得不提出，否认真主，但再优秀再好的人也不会得到真主的恩惠，他只能得到今世的幸福；另外，只是认真主，但做不到真主的主命的人，只能说是忙于今世忘记后世的人，他们也会受到一定的惩罚。因此说，认真主是信仰的钥匙。）<br />
&nbsp; &nbsp; 最终肯定有个结账的日子的（既是世界末日!）。几十年之后，不管是以前死了的还是当时死了的都将被复活。真主说道:「不然，他们否认复活时，我已为否认复活时者预备烈火。 」( 古兰经 25：11) 颂真主清净,一切赞颂全归真主,真主至大只依靠尊大的真主相助!<br />
之后，每个人按照在今世的所作所为得到相应的成绩，进而分到天堂或地狱。<br />
&nbsp; &nbsp; 「当大地猛烈地震动，抛其重担，说“大地怎麽啦 ?”在那日，大地将报告它的消息。因为你的主已启示了它；在那日，人们将纷纷地离散，以便他们得见自己行为的报应。行一个小蚂蚁重的善事者，将见其善报；作一个小蚂蚁重的恶事者，将见其恶报。 」（古兰经第99章）<br />
&nbsp; &nbsp; 「大灾的消息，确已降临你了，在那日，将有许多人，是恭敬的、劳动的、辛苦的，他们将入烈火，将饮沸泉，他们没有食物，但有荆棘，既不能肥人，又不能充饥；在那日，将有许多人，是享福的，是为其劳绩而愉快的，他们将在崇高的乐园中，听不到恶言，里面有流泉，里面有高榻，有陈设著的杯盏，有排列著的靠枕，有铺展开的绒毯。 」（古兰经第88章）<br />
主啊!求你教我们稳定在信仰上,教我们亡在信仰上,复生日你同着信仰复活我们,最仁慈的主啊,一切只凭你的慈惠! <br />
&nbsp; &nbsp; 后世从此开始，直接进入天堂的（包括先知(求真主赐福于全体圣人)，烈士，小孩等少部分）人永居其中，能亲眼见到真主！这是穆斯林追求的最大的荣誉！天堂也分等级，品级高的人可以和众多先知在一起，品级低的人同样会过美好的日子（提醒一下，今世的美好日子无法与其相提并论）；进入地狱的人，有的永居其中（既是否认真主的人），有的受完相应的惩罚再回到天堂（大部分人属于这一类，因为毕竟是人，难免不受今世的诱惑）。<br />
&nbsp; &nbsp; 我还听有人说：“穆斯林不怕死，如果因为正义而死算烈士，如果老死，最终也能进天堂。”这个观点我认为不全对，像正在世界各地发生的自爆现象属于自杀行为，伊斯兰教坚决反对。再说，最后进不进天堂那是真主说了算。因此说，我们能把握的只是短短的今生...<br />
<br />
&nbsp; &nbsp; 现在进入正题了，有人认为：“哎呀~2012年世界末日又咋了，死了也是一起死，大伙儿都在，挺热闹的”。还有人认为：“我才不信世界末日呢，就算有了那也是很遥远的事儿，和我无关，到时候我早就死了”。其实这两类人是最可怕的，也是最无知的。谁告诉你2012年会是世界末日？谁又给你保证了世界末日在你死之后？玛雅人？他们最近也不是否认了自己以前记载的那些！？<br />
&nbsp; &nbsp; 「他们只等待复活时忽然来临他们，复活时的征候确已来临了。当复活时来临的时候，他们的觉悟，对于他们那里还会有效呢！ 」( 古兰经 47：18)&nbsp; &nbsp;求真主保佑我们！<br />
&nbsp; &nbsp; 《古兰经》某些章节记载人们询问穆圣世界末日的时间：<br />
「他们问你复活在什么时候实现… 」( 古兰经 7：187)<br />
「他们问你复活的时间在什么时候实现？ 」( 古兰经 79：42)<br />
「真主命令穆圣回答说…只有我的主知道那个时候。 」( 古兰经 7：187)<br />
「我只凭真理创造天地万物，复活时是必定来临的。… 」( 古兰经 15：85)「 …他们是为复活时而恐惧的。 」(&nbsp;&nbsp;古兰经 21：49)<br />
求真主在今世赏赐我们美好的生活，在后世也赏赐我们美好的生活，求你保护我们，免受火狱的刑罚！<br />
<br />
&nbsp; &nbsp; 只有全能真主才知道末日的期限，人类是一无所知的。真主把末日的期限隐藏起来，是有原因的，这对于任何一个时期的人是有益的。假若人们知道末日的时间，他们将不会认真地思考，也不会留意《古兰经》中描述末日来临前的种种迹象。<br />
&nbsp; &nbsp; 那么，又怎么知道它的来临呢？我们只能通过《古兰经》以及《圣训》里边的迹象来推断。<br />
<br />
末日来临的最大迹象是穆圣的出生。当时的人们崇拜偶像，雕塑，山水天地等，完全脱离了尔萨圣人（耶稣）从真主那里降下来的《圣经》 （现在的基督教教徒手里的《圣经》有很多地方经人类篡改了）。最后，为了纠正那些异教，邪教，真主最后的先知穆罕默德圣人（求真主慈悯他、赐福他），统一了正教，并命名为“伊斯兰”。<br />
