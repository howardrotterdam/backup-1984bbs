　　华东冶金学院<br />
　　我们那的，听名字不错吧，后来改成安徽工业大学，一下子原形毕露了<br />
　　<br />
　　湖南工业大学<br />
　　有没有听过湖南工业大学？哈哈“知道内情的笑掉你大牙！！！<br />
