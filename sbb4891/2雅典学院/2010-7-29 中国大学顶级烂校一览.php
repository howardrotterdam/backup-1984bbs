<br />
　　西华大学<br />
　　谁都知道原来的“四川工业学院”是烂校一所，没想到它拉上个成都师专一下子就变成了这个校名，让填报志愿者无从知道它的实际水平了。<br />
　　<br />
　　南华大学<br />
　　应该说在地方高校里面，南华大学还是有一定实力的。但想到解放前的南华大学，总觉得衡阳的几所学校有点糟蹋这个校名。<br />
　　<br />
　　北方工业大学<br />
　　这所高校是分数最高的一般本科学校之一，但实力如何？我就不去揭人的老底了，反正它在网大的大学排名里名列第276位。(附带说一句，在这个排名里，它是和南华大学、南京财经大学并列第276位，而北京工商大学名列第311位) / 北方工业大学是比较垃圾，我当年差点儿就被它蒙了，我姨夫去考察过后说那以前就是一首钢的技校。。。<br />
　　<br />
　　南京财经大学<br />
　　这个校名听上去俨然一热门大学的感觉，其实只是几年前由一本(南京经济学院)一专(江苏财经高等专科学校)一**高校(江苏经济管理干部学院)合并后的产物而已。<br />
　　<br />
　　上海第二工业大学<br />
　　别以为它和以前的“上海工业大学”(已并入上海大学)有什么关系，这只不过是一所刚刚升为本科的普通高校而已，很奇怪的是它以前是**高校时是这个校名，专科学校时是这个校名，现在升为本科了还是这个校名。<br />
　　<br />
　　中国医科大学<br />
　　名字中带“中国”字样的大学都是同行业中数一数二的，但唯独它不是。<br />
　　<br />
　　首都经济贸易大学<br />
　　明显是抄袭对外经济贸易大学的牌子，还冠了个“首都”来标榜自己。近年来生源不错，看来牌子效益还是有用。<br />
　　<br />
　　北京青年政治学院<br />
　　名字听着很吓人吧，一不小心就让人联想到了大名鼎鼎的“中青政”，但其实只是一所专科学校而已，不道为什么要取个这种混淆视听的名字。<br />
　　<br />
　　北京工商大学<br />
　　北京人都知道，这就是一烂校。但偏偏校名取得不错，又是“北京”又是“工商”，结果成了每年分数线最高的一般本科学校之一(有一年在某省竟然创下了近630分最低录取线的夸张纪录)。幸好今年被教育部黄牌警告了一下，可以让人对其提高点警惕。<br />
　　<br />
　　华东交通大学<br />
　　这个名字浪费得也有点过分，现在取个带“华东”这种字样的校名可不容易，不知道当初教育部是怎么批准它的。<br />
　　<br />
　　东华大学了<br />
　　原来叫中国纺织大学挺好，好端端的一所重点大学偏偏改了这样一个破名字，跟那些什么西华阿、南华听起来都差不多，真是自掉身价！<br />
　　<br />
　　南京信息工程大学<br />
　　这所学校原是南京气象学院，是一所以气象为核心的学校，其他专业一塌糊涂。怎么改了信息工程，真是驴唇不对马嘴！<br />
　　<br />
　　成都信息工程学院<br />
　　原名是成都气象学院，这气象学校都怎么了！<br />
　　<br />
　　南京气象学院<br />
　　本来还挺好，现在好象改成中国工程信息大学，搞的有点不伦不类,南气还是相当不错的，名气还挺响，现在一改谁知道谁是谁了<br />
　　<br />
　　长江大学<br />
　　怎么听都象是个民办学校,其实不是,是以前的江汉石油学院和几个学校合并的<br />
　　<br />
　　沈阳理工大学<br />
　　名字狠学校lan的<br />
　　<br />
　　天津工业大学<br />
　　不知道害了多少人呢<br />
　　<br />
　　长安大学<br />
　　当属改名最失败的大学！想当初“西安公路学院”多亮的名头！如今毕业生求职时还要在罗嗦一句：就是原来的西安公路学院。生怕人家以为是哪所民办院校。<br />
　　<br />
　　渝州大学<br />
　　超级超级烂，我在重庆住了十年才听说还有这样一所大学，还好这所大学好象还不对重庆以外的省市招生<br />
　　<br />
　　渤海大学啊<br />
　　原来的锦州师范学院和辽宁高等商业专科学校合并的。出去找工作人家以为是民办，只好加句，原来的锦州师范学院<br />
　　<br />
　　河北经贸大学<br />
　　……全是眼泪…..<br />
　　<br />
　　中南科技大学<br />
