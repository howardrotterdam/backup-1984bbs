　　<br />
　　北京信息工程大学<br />
　　巨垃圾 我在北京上学 知道的就是破本科 连研究生院都没有<br />
　　<br />
　　金筑大学<br />
　　知不知道贵阳有所金筑大学？靠，名字好听。还“金子筑成的大学呢”。一个破专科，里面卫生脏得要命！教学条件无比的差！！<br />
　　<br />
　　鲁东大学<br />
　　也就是传说中的烟台师范学院<br />
