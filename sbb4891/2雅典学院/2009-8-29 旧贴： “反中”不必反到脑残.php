																											<div id="postmessage_219137" class="t_msgfont">一点五<br />
&nbsp; &nbsp; <br />
从标题的文本看，属于左愤的工作领域，然而左愤虽多，却缺乏表现主题所具备的技术含量，故而决定亲自操刀，干涉一回左愤们的内政。<br />
<br />
本人早期被左愤们视为“幼右”，现在是“极右”。虽然有进步因子在里面，但后者是高抬了，俺一直没能修炼到那个高度，今天准备反戈一击的对象，正是那些所谓以自由主义标榜的“极右”分子。<br />
<br />
思想无禁区，言论有底线。每一个社会都有极右分子，日本那些发表彻底否定侵华战争、否定南京大屠杀言论的，就是日本的极右。然而在日本这样高度民主的国家，这类人也经常因为发表此类言论而丢官去职。言论是也有自己的边界，正如自由受到法律的约束一样。<br />
<br />
网络上确实活跃着一小撮极右分子，正是这些人让网民蒙上了“境内敌对势力”的不白之冤。因为当局者由于自己的无能与愚蠢，无力鉴别“真右”与“极右”，于是干脆一棍子打死。路遥知马力，事久见人心。鉴别极右，光靠“路遥”与“事久”还不行，它需要突发事件。突发事件是一面照妖镜，让极右暴露无遗。<br />
<br />
去年拉萨3&#8226;14事件，是中国极右分子的第一次大曝光。这些人如同先知一样解读中国政府的每一个动作：军警上街后它们说动作迟了，故意让事态扩大好为自己制造“镇压”借口；暴徒闹事的图片发布后，他们指出其中有人是“军人假扮”；事件平息后，他们论证出3&#8226;14事件是为了替奥运安保“造势”……。<br />
 <br />
乌鲁木齐7&#8226;5事件发生后，同样的事情再次重演，这一次尤其让人所不耻：警力没有迅速到场是当局想把事情搞大，好“镇压”，好“抓人”；警察出动后，它们看到的只是“枪声”；死亡140人的消息一发布，它们不是悲哀无辜的生命，而是强调其中有“维人”；图片发布后，它们看到的不是暴徒的残忍，而是场面“血腥”；宣布戒严后，它们又发现头一天就该如此；人们出于义愤上街后，它们不排除游行队伍里有“党人”；事件尚未平息，“一场失败的政治阴谋”论即已出台……。同为中国人，何必如此狠毒？<br />
<br />
尽管极右分子们的高论一个接一个，但没有一个人能说到乌鲁木齐7&#8226;5事件的真正教训上去，这个教训就是：****并不能保持社会稳定。此前这曾被计算为中国模式的成绩之一。<br />
<br />
李敖有个观点：尽可愤世嫉俗，但是在心坎里还是要有一股爱，而不是恨，目的不是在逞一时之快，不在“灭此朝食”似的要打倒别人。我们需要这个胸襟，今天的社会离不开这个胸襟。否则折腾来折腾去，换来的同样会是假共和。<br />
<br />
今天的中国确实有后发优势，体现在“逢中必反”上，就是我们可以尽情地吸取别人的经验教训。<br />
<br />
很多人可能忽略了这样一个事实：波兰颜色革命后，第一任民选总统并非团结工会主席瓦文萨，而是前共产党总书记雅鲁泽尔斯基。这是波兰民族的最让人感叹之处：瓦文萨立即上台，波兰必然会出现“清算”；雅鲁泽尔斯基当总统，则可实现前政权与新政权的“和平过渡”。前者固然痛快，但于一个社会、一个民族、一个国家肯定不意味着和谐。雅鲁泽尔斯基当选总统一年半后，主动宣布提前大选，瓦文萨也终于如愿以偿，当上了总统。波兰就这样极其平和地实现了自己的历史转型。<br />
<br />
把执政者想像得越邪恶，对抗必然越激烈，社会也势必越动荡，这其中，每一个人都会成为受害者。这也是波兰颜色革命的精神领袖米奇尼克的一个观点：必须学会与执政者共同行动。进化到这一步很难，但这一步必须跨跃。</div>

							
							
							
							
