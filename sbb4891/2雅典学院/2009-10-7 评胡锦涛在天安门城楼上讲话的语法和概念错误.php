 <br />
中共中央总书记、国家主席、中央军委主席胡锦涛检阅受阅部队后，在天安门城楼上发表讲话，以下是全文实录。<br />
<br />
[胡锦涛]:全国同胞们，同志们，朋友们：今天，我们隆重集会，庆祝中华人民共和国的成立60周年。<br />
<br />
(评)语法错误:全国同胞们已经包括了同志们，朋友们，高考肯定扣分。 <br />
<br />
[胡锦涛]:在这个喜庆而又庄严的时刻，全国各族人民都为伟大祖国的发展进步感到无比自豪，都对实现中华民族伟大复兴的光明前景充满信心。在这里，我代表党中央、全国人大、国务院、全国政协和中央军委，向一切为民族独立和人民解放、国家富强和人民幸福建立了不朽功勋的革命先辈和烈士们，表示深切的怀念！ <br />
<br />
(评)国家主席无权代表全国人大，全国政协，这是基本常识。<br />
<br />
[胡锦涛]:向全国各族人民和海内外爱国同胞，致以热烈的祝贺！向关心和支持中国发展的各国朋友，表示衷心的感谢！ <br />
<br />
(评)语法错误:全国各族人民和海内外爱国同胞可以“祝贺”国庆，但不能“热烈的祝贺”全国各族人民和海内外爱国同胞。 <br />
<br />
[胡锦涛]:60年前的今天，中国人民经过近代以来100多年的浴血奋战终于夺取了中国革命的伟大胜利，毛泽东主席在这里向世界庄严宣告了中华人民共和国的成立。<br />
<br />
(评)语法错误:近代以来100多年，算术不好还是历史不好？<br />
<br />
[胡锦涛]:中国人民从此站起来了，具有5000多年文明历史的中华民族从此进入了发展进步的历史新纪元。60年来，在以毛泽东同志、邓小平同志、江泽民同志为核心的党的三代中央领导集体和党的十六大以来的党中央领导下，勤劳智慧的我国各族人民同心同德、艰苦奋斗，战胜各种艰难曲折和风险考验，取得了举世瞩目的伟大成就，谱写了自强不息的壮丽凯歌。 <br />
<br />
(评)概念错误:具有5000多年文明历史的不是中华民族，而是汉民族<br />
<br />
[胡锦涛]:今天，一个面向现代化、面向世界、面向未来的社会主义中国巍然屹立在世界东方。新中国60年的发展进步充分证明，只有社会主义才能救中国，只有改革开放才能发展中国、发展社会主义、发展马克思主义。中国人民有信心、有能力建设好自己的国家，也有信心、有能力为世界作出自己应有的贡献。 <br />
<br />
(评)概念错误:中国现在不是社会主义，救中国是因为中国开始搞资本主义<br />
<br />
[胡锦涛]:我们将坚定不移坚持中国特色社会主义道路，全面贯彻执行党的基本理论、基本路线、基本纲领、基本经验，继续解放思想，坚持改革开放，推动科学发展，促进社会和谐，推进全面建设小康社会进程，不断开创中国特色社会主义事业新局面、谱写人民美好生活新篇章。我们将坚定不移坚持&quot;和平统一、一国两制&quot;的方针，保持香港、澳门长期繁荣稳定，推动海峡两岸关系和平发展，继续为实现祖国完全统一这一中华民族的共同心愿而奋斗。 <br />
<br />
(评)语法错误:“基本路线”可以贯彻执行，“基本理论”怎样贯彻执行？<br />
<br />
[胡锦涛]:我们将坚定不移坚持独立自主的和平外交政策，坚持和平发展道路，奉行互利共赢的开放战略，在和平共处五项原则基础上同所有国家发展友好合作，继续同世界各国人民一道推进人类和平与发展的崇高事业，推动建设持久和平、共同繁荣的和谐世界。中国人民解放军和人民武装警察部队要发扬光荣传统，加强自身建设，切实履行使命，为维护国家主权、安全、领土完整，为维护世界和平再立新功。 <br />
<br />
(评)语法错误:只能坚持走和平发展道路，如何“坚持和平发展道路”<br />
<br />
(评)概念错误:中国人民解放军和人民武装警察部队主要任务之一是保障人民生命和财产安全　　<br />
<br />
[胡锦涛]:历史启示我们，前进道路从来不是一帆风顺的，但掌握了自己命运、团结起来的人民必将战胜一切艰难险阻，不断创造历史伟业。展望未来，中国的发展前景无限美好。全党全军全国各族人民要更加紧密地团结起来，高举中国特色社会主义伟大旗帜，与时俱进，锐意进取，继续朝着建设富强民主文明和谐的社会主义现代化国家、实现中华民族伟大复兴的宏伟目标奋勇前进，继续以自己的辛勤劳动和不懈奋斗为人类作出新的更大的贡献！ <br />
