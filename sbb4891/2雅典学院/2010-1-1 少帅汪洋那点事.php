													<h2>少帅汪洋那点事</h2>
						
						
																											<div id="postmessage_408526" class="t_msgfont">来自人民网<br />
<br />
<a href="http://politics.people.com.cn/GB/1026/10688556.html" target="_blank">http://politics.people.com.cn/GB/1026/10688556.html</a><br />
<br />
　作为“少壮派”官员，汪洋的仕途轨迹，似乎平淡无奇，但其出场，却显得异同寻常。<br />
<br />
　　政情人士分析，汪洋曾历任安徽省副省长、国家计委副主任、国务院副秘书长、重庆市委书记、广东省委书记，在地方打滚过，也在中央历练过，既有鲜明的“团系”色彩，又是难得的财经好手，前途无可限量，未来当有想象空间。<br />
<br />
　　有消息指，2007年十七大前夕，各地官员忙于宣传造势之际，汪洋也没有封锁“十一”黄金周期间重庆发生客车爆炸、导致27人死亡的消息；加上早前引发中外关注的重庆“最牛钉子户”拆迁问题，最终通过谈判方式得以妥善解决等等，都令他在民众和媒体的心目中加分，无疑增加了其在政坛的筹码。<br />
<br />
　　一位认识汪洋的人士如此评价：其升迁之快，与他本人严以律己、积极进取的做人风格密不可分。据称，在汪洋担任安徽省体委主任后，他的一位老朋友到合肥办事，想和他叙叙旧，汪当时已有自己的专车，却骑着自行车到火车站接他。<br />
<br />
　　不过，熟悉他的人表示，这远远不是汪洋性格的全部，工作中的汪洋表现出的超前思维和果敢作风，在他出任安徽省铜陵市长，真正开始主政一方时展现得淋漓尽致。<br />
<br />
　　铜陵改革<br />
<br />
　　“娃娃市长”是汪洋在任职安徽铜陵市长时，当地政界及老百姓对他的戏称。<br />
<br />
　　汪洋于1988年11月出任安徽省铜陵市委副书记、代理市长。铜陵是个较小而且贫困的省辖市，在安徽并无特别重要的地位，但在铜陵4年，汪洋雷厉风行，颇具改革和创新意识，并做了一件轰动一时的事情，震惊海内外。<br />
<br />
　　1991年11月14日，《铜陵日报》发表署名龚声的文章《醒来，铜陵！》，呼吁“改革大潮汹涌澎拜。历史不允许我们躺在计划经济上酣睡了。必须解放思想，向一切僵化、陈腐、封闭的思想观念开刀”一场有关思想解放大讨论的序幕轰轰烈烈地拉开，在整个铜陵激起强烈反响。<br />
<br />
　　此文犹如平地惊雷，由这个贫穷的皖北山区传遍了神州大地。而汪洋就是这件事的始作俑者。<br />
<br />
　　令人振奋的是，15年前在皖北山沟吹响的“解放思想”的号角，在15年后汪洋主政之下的南粤大地再次响起。这不能不说是一种历史的巧合。但与其说是历史的巧合，不如说是历史的必然。<br />
<br />
　　汪洋当时在电视讲话中说：要改变观念！有人“养鱼怕偷”，“做生意怕骗”，就是不怕穷。这与此后中共领导人邓小平著名的南方谈话思想很接近，显示出汪洋敏锐的政治嗅觉。而铜陵的做法也引起了中央高层，尤其是邓的关注。<br />
<br />
　　主政铜陵时，汪洋还对当地旧体制进行了大刀阔斧的改革：打破“三铁”（铁饭碗，铁交椅，铁工资），且住房制度、用工制度、干部管理、机构设置等等都被列入改革计划，多家全民国有企业列入改革范围。整个改革被推上高潮。<br />
<br />
　　但是，这些改革举措很快就招致了许多保守势力的反对。<br />
<br />
　　为了平息这场纷争，中央高层放话：可以先行试点。此后，《人民日报》还发表了一篇社论：《醒来，不仅仅是铜陵！》<br />
<br />
　　对是次改革，汪洋自己评价：拟定方案，难得出奇；启动实施，顺得意外。而铜陵百姓也对他这个“娃娃市长”的做法从怀疑到逐渐认可，并评价他：聪明能干、思维超前、魄力很强、做事果敢。<br />
<br />
　　铜陵改革的成功让世人瞩目。中国青铜文化节即是从1992年开始的，汪洋当时的主旨是：文化搭台，经济唱戏。直至今日，铜陵人依旧会不由自主地把后来的铜陵市长与汪洋作对比。<br />
<br />
　　“铜陵改革”让汪洋获得了邓小平的赏识。有说法称，邓南巡回京途中在安徽蚌埠停留，并特别召见了汪洋。也有香港出版的相关图书如此描述：“1992年邓小平南巡来到安徽合肥，接见安徽有关领导，时任铜陵市长的汪洋也被点名要求参加。会见后，邓对汪印象深刻，认为其是个人才。”<br />
<br />
　　知情人士透露，关于邓小平接见汪洋的说法版本不一，很难有人能准确判断，但有一点是可以肯定的，那就是邓显然在见汪洋前就已注意到他。<br />
<br />
　　果然，此后不久，汪洋即调离铜陵，担任安徽省省长助理兼省计委主任。1993年，38岁的汪洋出任安徽省副省长，成为当时全国最年轻的副省长，并继而进入省委常委。<br />
<br />
　　在皖省工作期间，汪洋鲜明的施政风格，给当地官场留下了深刻的印象。据称，汪洋的超前意识很强，在任常务副省长分管财税工作时力推一项改革，连时任国务院总理的朱F基都说他“年纪不大、胆子不小”。<br />
<br />
　　很少有人知道，汪洋还是个孝子。<br />
<br />
　　但自从离开宿县以后，汪洋就很少回到家乡。尤其是后来，他也想把母亲接到合肥、北京长年居住，但都被母亲婉言拒绝了。汪洋在安徽省任职期间，每年春节都要回宿县看望母亲，母亲认为路费很贵，表示打个电话就可以了，不必每年都回。<br />
<br />
　　汪洋到北京工作以后，时间更加紧张，回老家的机会也愈发稀少。6年间，只有一次春节回家看望母亲，而且还是悄悄地，没有带秘书，也没有惊动地方政府，汪洋自己乘坐火车从北京回到了宿县。过了一个晚上，第二天他又悄悄地离开了。<br />
<br />
　　知情者说，整个过程非常安静，即使他们家的邻居也没有感觉到，他的母亲也没有跟任何人说起过。<br />
<br />
　重庆治吏 <br />
<br />
　　由于汪洋过硬的政治素质和干练、超前的改革思维，再加上其有国务院三峡工程建设委员会委员的任职经历，使得中央在考虑西南重镇重庆市委书记的人选时，迅速锁定汪洋。<br />
<br />
　　记者多方调查获悉，国家在1999年提出西部大开发战略后，致力缩小东西部发展差距，但收效并不明显。重庆作为长江中上游的核心城市和西部大开发战略结合重点，急需一个年富力强、有魄力、有能力、有眼光、能把握大局的当家人来规划和协调大的区域经济整合，汪洋正是合适人选。<br />
<br />
　　2005年底，汪洋空降重庆，正式履新。据信，这一人事任命是考量其曾在国家计委工作，熟悉经济课题，并参与制订“十一五”规划，对中国经济未来5年的发展脉络非常清楚，可助力这座中国最大直辖市新阶段的发展。<br />
<br />
　　此外，中央曾为解决三峡库区百万大移民等因素而决定重庆直辖。8年间，重庆主城区获得了发展，但三峡库区却困难重重。而在处理库区矛盾方面，汪洋富有经验。2004年，四川大渡河兴建瀑布沟水电站，汉源县约10万人将被迫迁移。由于居民不满政府的土地征收赔偿方案而引发抗议，并与警方发生冲突。当时，就是汪洋带着胡温的四点重要指示前往汉源平息。<br />
<br />
　　平息“汉源事件”，让外界对汪洋的手腕与才干评价颇高。<br />
<br />
　　当地官员称，汪洋对中央三峡库区的政策了解透彻，也因做过宣传工作，他知道如何把握舆论，让全国知道重庆在三峡工程中所作的牺牲，从而争取到中央和其他省市对三峡的支持和投入。<br />
<br />
　　在汪洋的强力治理下，截至2007年4月，重庆已迁移三峡库区移民102.4万人，完成了113.8万人任务的90％。汪洋当时自豪地说，“对于三峡百万移民这个世界级难题，我们已是胜券在握了。”<br />
<br />
　　汪洋到任后，重庆的一个重大变化就是，重庆官风被严厉整顿。<br />
<br />
　　一个耐人寻味的细节是，在重庆2006年遭遇特大旱灾时，汪洋到农贸市场视察，菜农坐在地上回答他的问题，随行人员便态度傲慢地两次叫菜农站起回话。汪洋突然做出了一个出人意料的举动，他从菜农箩筐里抓起一个大青椒，转身砸向该工作人员，事后表示：“今天我对有些工作人员不客气，干什么要群众站起来说话？我们是人民的公仆，站着的当然应该是我们！”<br />
<br />
　　也有人指汪洋是在作秀。但后来的一些事实让这种论调很快破产。<br />
<br />
　　一个突出事件就是，震惊2007年中国新闻界的“最牛钉子户”事件。<br />
<br />
　　汪洋最广为人知的事情是以开明、冷静、务实的态度解决了重庆“最牛钉子户”事件。当时，“钉子户”户主杨武爬上孤岛般的楼顶大喊：我要见汪洋！<br />
<br />
　　舆论一下子将汪洋推到了风口浪尖。<br />
<br />
　　汪洋没有寻求有关部门封锁媒体的报道，没有动用执法部门“硬来”，而是指示各方与户主积极谈判，并最终以一种理性、和平的方式圆满解决了此事，赢得了国内外媒体的高度称赞。<br />
<br />
　　有媒体说，汪洋此举为各级地方政府解决同类纠纷提供了一个可资借鉴的模板。还有媒体甚至戏称，汪洋通过了“考试”并获高分。<br />
<br />
　　香港《亚洲周刊》评价说，“最牛钉子户”结局皆大欢喜，汪洋为胡温强力冲关通过的《物权法》、为大陆私权保护，写下了一个理想的注脚。<br />
<br />
　　而就在“最牛钉子户”事件被炒得沸沸扬扬之际，汪洋的另一个重大举动在山城重庆火爆出炉。<br />
<br />
　　2006年年底，重庆市出台了一份传媒改革文件，在全国首开先例。文件要求，重庆市委书记（市人大常委会主任）、市长、市政协主席除涉及全局性重要活动以外的常规性调研活动，《重庆日报》二版刊发消息，字数原则上不超过1000字；广播、电视报道不上头条，长度原则上不超过3分钟。<br />
<br />
　　汪洋的一句“让读者习惯到二版找汪洋”，让重庆新闻界至今仍记忆犹新。<br />
<br />
　南粤新政<br />
<br />
　　2007年，任职重庆两年之后的汪洋，奉调南下，主政广东。<br />
<br />
　　汪洋履新广东时曾表示，广东要继续走在全国的前列，走在思想解放的前列，走在改革开放的前列。<br />
<br />
　　不过，汪洋所面对的挑战不少，因为今天的广东已不是以前那个拥抱改革，时刻希望走在前沿的广东了，而是官场积弊重重。所以，汪洋要使广东再次成为改革开放的排头兵，就必须另辟蹊径。<br />
<br />
　　当年邓小平铺开改革新局时，广东也是他十分重视的地方。杨尚昆、任仲夷、习仲勋、吴南生等中共元老，均曾主政广东，力推改革。1992年邓小平向极左阵营开火，为市场化改革打气，也是通过南巡广东实现。<br />
<br />
　　在就任广东省委书记后召开的第一次省委全会上，汪洋就给各级官员以“当头棒喝”，一口气说了22个“解放思想”，并号召各级领导发扬当年“杀开一条血路”的精神。<br />
<br />
　　此外，汪洋还大力推动“双转移”经济战略，即进行全省产业转移和劳动力转移，把落后的密集型企业驱赶到广东偏远地区，然后在原有的地方进行产业升级。汪洋还主张企业“该倒的就让它倒”，让发达起来的企业去施惠普罗大众，问题是，受到全球金融海啸牵连，广东近来有五万多家企业倒闭，庞大的失业潮迫使总理温家宝站出来表态“该扶的就要扶”，这种中央与地方互杠的罕见现象，令外界多所揣测。<br />
<br />
　　广东媒体甚至报导，在去年举行的广东省特区工作会议上，汪洋还坚持说，“不管别人怎么说，双转移要坚决，‘腾笼换鸟’要坚决。千万不要因为要保增长，不管什么都继续上”。<br />
<br />
　　与“解放思想”和“腾笼换鸟”相伴随的，则是广东官场的持续震荡。<br />
<br />
　　知情人士透露，广东虽为改革开放排头兵，但本土官员的保守意识却由来已久。<br />
<br />
　　比之更甚的，则是广东官场的贪腐之风。<br />
<br />
　　2009年，广东爆发官场大地震，原广东省政协主席陈绍基、省纪委书记王华元、深圳市长许宗衡因严重违纪，被中纪委革职查办，广东官员人人自危。知情人士表示，陈、王、许的落马，与汪洋的强势治贪有关。<br />
<br />
　　另有消息指，汪洋治贪，曾得中南海意旨，中央政法委书记周永康亦曾亲赴广东，坐镇督办治贪查黑。<br />
<br />
　　香港媒体指，解放思想，整肃官场，是汪洋主政广东后的得意之作。但广东本土官员对汪洋阳奉阴违、消极支持，使得汪洋治粤阻力重重。<br />
<br />
　　分析人士说，查办贪腐案，对于汪洋来说，既是一次政治考验，也是一次政治契机。汪洋如果在其任内大力刷新广东吏治，引领广东经济更上层楼，其仕途再上一个台阶亦未可知。<br />
<br />
　　观察人士分析，广东官场历来排外倾向严重，所以中央空降省委书记时，通常以兼任中央政治局委员的方式来强化其权威，以便开展工作。而汪洋是政治局中资历最浅的委员，虽然有安徽、重庆工作的地方经验，但这些省市的地位仍难与广东相提并论，故部分广东官员对其持听其言、观其行的态度，看他到底能否压得住阵脚，能否带领广东走出新天地。<br />
<br />
　　正因如此，在一次省委会上，汪洋还痛斥广东官员的优越感，要求他们克服自满情绪，增强忧患意识；克服狭隘视野，树立世界眼光；克服见物不见人的观念，坚持以人为本。同时，汪洋还痛批50名代表缺会，要求纪委严查，此举迅速震慑广东官场。<br />
<br />
　　时至今日，多方事实证明，汪洋治粤已显成效。<br />
<br />
　“个性官员”VS“资深网友”<br />
<br />
　　有人士分析，人口众多的中国从来都不缺官员，缺的只是个性鲜明、敢于直言、有所作为的官员。而汪洋就是这样一位难得的“个性官员”。<br />
<br />
　　汪洋的个性表现在，敢想，敢说，敢做，敢为，敢爱，敢恨，敢背弃官场规则，敢与上级意旨相争辩，不按官场常理出牌。<br />
<br />
　　一个典型事件是他对广东媒体的批评。<br />
<br />
　　当地媒体为了抢风头，争先报道汪洋的调研。但汪洋对这些报道也“烦”了，说：“我下去调研，跟群众交谈，这有什么新闻性？也真难为了摄影记者，每次要拍出不同姿势，我又不是体育运动员，运动员摆不同的姿势还有意义，我拍来拍去不就那个样子吗？有些报道里，大会才刚开，就说‘大会一致认为’，这不是糊弄群众吗？”<br />
<br />
　　汪洋的一番话，对当地一味跟风的媒体无异于当头棒喝。<br />
<br />
　　除了是一名个性官员，汪洋还声称自己是一名“资深网友”。<br />
<br />
　　2006年，汪洋被评为“年度网络知识分子”。这一消息成为2006年度网络界最热新闻之一。许多网友“挖掘”到，汪洋与网络竟有深厚的渊源！<br />
<br />
　　汪洋到重庆上任后的一系列执政措施和理念深得重庆网民赞赏，被网友称为“汪帅”；2007年重庆经济工作会上，汪洋使用笔记本电脑开会，6月汪洋公布个人电子邮箱向全球网友征集建设“新特区”的意见，并两次约见提意见的网民，20多次以个人名义回复提意见的网民。<br />
<br />
　　主政广东后，汪洋继续延续了其“资深网友”的品格。<br />
<br />
　　中央某大报资深评论员与汪洋曾有过接触，他对汪洋的做派深有感触。<br />
<br />
　　该评论员说，2008年5月17日，他应邀到岭南大讲坛作了一次题为《改革开放以来的四次大争论》的演讲，广东有人觉得汪洋的“解放思想”有点空，但他当即否定了这种说法。他讲述了一个他在广东切身经历的事实：湛江书记公开述职大会。一把手述职各处都有，但湛江很不同。湛江的做法是，述职大会之前，11位一把手的述职报告都在新浪网发布，欢迎网友“拍砖”。<br />
<br />
　　重要的是，这个大会，从始至终都由湛江电视台和当地网站同步直播、全程报道，湛江市民都能看到。<br />
<br />
　　令该人士特别感慨的是，市委把11个市区县的网友对述职报告的“拍砖”打印成册，发给与会人员。网友对当地情况很是了解，有些评论很不客气，连他自己都有点受不了。<br />
<br />
　　湛江的改革探索随即在全国产生重大影响，中央某大报还进行专题报道，给予充分肯定。<br />
<br />
　　而就在湛江市一把手公开述职之后的第五天，2008年4月17日，汪洋和省长黄华华在广州珠岛宾馆和26位网友进行了一次座谈。汪洋的开场白就颇具网络特色：“过去你们在网上‘华山论剑’，今天我们在珠岛‘当面拍砖’。”</div>

							
							
							
							
