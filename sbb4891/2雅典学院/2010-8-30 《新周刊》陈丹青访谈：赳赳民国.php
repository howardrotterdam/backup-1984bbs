													<h2>《新周刊》陈丹青访谈：赳赳民国</h2>
						
						
																											<div id="postmessage_804618" class="t_msgfont">《新周刊》访谈：赳赳民国 <br />
非文学 创作 <br />
<br />
在一次交谈中，您提示说：民国范儿并不像现在的影视剧那样，但可以到民国电影中去找，请进一步说说。<br />
<br />
陈：我喜欢看样子。所谓＂民国范儿＂，先是一种“样子”吧，和如今满眼所见不一样。今人要“看”民国，只能是照片和影像了。去年的电视剧《潜伏》，有点像的，但民国的真滋味还在民国老电影：《马路天使》、《小城之春》、《神女》，《一江春水向东流》……那时的导演和演员不知道什么“民国范儿”，他出来就是啊。<br />
　　你们新周刊今年发了一幅难得的照片，是胡适在美做大使，几个绅士炫围着他，各人的装扮，姿态，室内的陈设，全是对的――单是这张照片，可写一篇民国与共和国文化差异的大论文――可是拍摄那一刻，他们哪在乎民国不民国。现在各驻外使馆，你见过吗？<br />
&nbsp; &nbsp; 近年拍的所谓主旋律电影，那份肉麻，我宁可看五六十年代的《南征北战》、《鸡毛信》、《董存瑞》，一股活气：那才是货真价实的革命电影。“革命范儿”，也早没啦。你听听现在唱的老歌红歌革命歌，别说装腔，靡靡之音也不如，那是革命的自我调戏、自我作贱啊。<br />
&nbsp; &nbsp; 正宗的革命范儿，是民国之前的国民党，当时俗称革命党。革命党闹革命，没功夫弄文艺，所以民国文艺倒是民间生发的，有感情，有豪气，但是没党气。听过１９５３年前后电影《上甘岭》里的大合唱《祖国万岁》吗？至今还是歌颂共和国的压轴曲：<br />
&nbsp; &nbsp; <br />
一条大河波浪宽，<br />
&nbsp; &nbsp; 风吹稻花香两岸，<br />
&nbsp; &nbsp; 我家就在岸边住，<br />
&nbsp; &nbsp; 听惯了船工的号子，看惯了船上的白帆……<br />
<br />
&nbsp; &nbsp; 这种歌词的写法、爱国的爱法，其实是民国的。当时的词曲作者与合唱演员，是民国人，歌声里那种情感，也是民国式的，此后这等朴素真挚的歌词硬就是写不出来――到六七十年代，革命歌一股戾气，现在的唱法，那是又土又俗的妖气了。<br />
<br />
<br />
我们想像中的民国范儿属于一种误读？您曾经说，民国范儿到文革才结束，中共高层都有民国范儿。<br />
<br />
陈：别以为民国范儿属于“反动派”，弄得装扮蒋介石毛人凤的演员们挤眉弄眼瞎琢磨，其实第一代第二代中共高层站那儿，就是一群民国人。毛泽东1893年生，民国元年十九岁，1949年五十六岁。你把五十年代中南海照片和国民政府的黑白照片对比看，何应钦啊、李宗仁啊……党气虽有不同，“范儿”大致一类。国共仇寇两度合作，原是同学同事关系，平时隔壁邻居，白天在同一个办公室上班，追同一个女子，一家两代就有两党：邵力子傅作义陈布雷的儿女，都是中共的人。蒋经国在苏联还写过公开信声讨他爹。毛周临死惦记对岸的故人，那都是老上级老朋友啊。<br />
&nbsp; &nbsp; 两党作风彻底改变，是到文革了。部分原因是服装的变化。蒋比毛早死一年，同期的照片，蒋宋夫妇穿中山装和旗袍，大陆这边，毛江二位忽然穿上解放军绿军装，那是共产党自己设计的，民国时期，共军军装就是国军军装嘛，只是朴素破旧一点，几位大帅不戴美式大盖帽而已。共军是到抢占东北后才设计自己的军装样式吧，这要考证，我不清楚。然而服装和扮相是要命的事情。民国二十年代的热血青年向往一套北阀军装，四十年代的时髦小子穿美式大衣，六十年代末，哪个红卫兵小子穿一套黄里泛白的解放军旧军装，姑娘瞧见，就扛不住了。美国普普艺术和法国学生运动都把绿军装视为红色中国的符号，八十年代崔健单挑绿军装上台喊摇滚，不是没道理，照符号学观点，那才是正宗共和国小子，一无所有。<br />
&nbsp; &nbsp; 你会说，服装不能代替气质，没错。国民党元老不去说了，共产党起事那拨人，都是有脸有谱的范儿：朱德是忠厚的军阀气，周恩来是辅佐的宰相气，李大钊是典型的儒相，瞿秋白是刻骨的亡命书生气，陈独秀根本就是康梁那代大逆子，生得晚了，气概一点不输，犹有过之……二流的角色也是有声有色：康生那张明末东厂脸，许世友简直是明初的武夫相……搁在古代，这些脸谱可就进了三国水浒，说书唱戏作演义了。<br />
&nbsp; &nbsp; 如今的军政舞台，你排几个像样的脸谱给历史看看。１９４９年第一届政协会议老照片，我从毛周身后的人缝里仔细瞧，各省民主党派那些老人的面相架势，如虎如豹，都是真角色，满以为从此可以协商下去呢。后来一批批蔫了，但譬如章士钊，还给用着，还常活动：早先他是陈独秀的辩护律师，又暗送经费给毛润之，念老交情，文革初他还试图协调毛刘关系，文革中期周恩来安排他密使香港和国民党人员接茬……<br />
&nbsp; &nbsp; 文革后，民国“范儿”沉渣泛起了：很多民国老人都还活着呢。<br />
<br />
<br />
依您总结，民国范儿是个什么范儿?<br />
<br />
陈：１９７９年我在北京的什么演出场合远远看见当时的侨联主席廖承志。迟到了，穿着肥大宽松的中山装裤，一脸疲倦而宽厚的官相，被前呼后拥走过座位当中的通道，和人握手点头，谈笑风生，十足像个老爷。你想啊，虽然他在共和国做了三十年大官，但他爹是民国元老，他是第一代民国老革命的公子哥，大少爷，从小看惯两党大老，自是民国的气度。前年读到一篇他的下属的回忆，果然说他一天到晚开玩笑，为此还做检讨，检讨时仍旧开玩笑，说是临死前再说一句，逗大家笑笑，然后跳进棺材去。<br />
&nbsp; &nbsp; 这就是民国范儿。如今的高官会是这般做人说话吗？<br />
&nbsp; &nbsp; 可是老牌共产党员有的是这范儿。单是特务系统，李克农喜欢养狗打猎，康生在延安穿美式皮夹克，还精于搜刮文玩（最近去世的漫画家华君武也会打扮，叼个烟斗，皮大衣敞着，雪白的羊毛围巾，他在延安时期的照片穿着破棉袄，可是一脸神色是上海滩前卫艺术家公子哥）。周恩来不必说了，重庆南京时期，七十年代中美建交时期，美国人见那范儿，就有认同感。周的父祖辈是被选派迎候南巡圣上的地方豪绅，所以这位“无产阶级革命家”其实是晚清的世家子弟。如今外交官见外宾，全套西装领带，头发专门弄过，还是又土又呐，放不开。前时退休外交官吴建民指说驻外官员说话言语贫乏，其实很难怪的：二十年来，再高层的官员学者也是小科员一路看眼色混上来，谈吐气象，自是不济。<br />
&nbsp; &nbsp; 但民国范儿并不单指权贵，而是各色人等坦然率真那股劲。民国前后出来举事的家伙，敢作敢为，有豪情，有胆气。成败不论，忠奸另说，你譬如汪兆铭，诗词了得，美少年，居然弄炸弹，搞暗杀（蔡元培也干过同样的事），捉住判死，清朝官员念他才俊，给他免了――清朝的范儿也是性情毕露啊――再譬如胡兰成，浙江乡村穷孩子，学历背景全没有，出来指点江山，有学问有文采。现在嵊县胡村出来个穷小子，也就是打打工，写写手机短信吧……民初张国焘陈公博他们去广州，年纪轻轻，满脑子革命见解，廖仲凯，就是廖承志他爹，干瘦老头，直接带着小伙子进国民政府面见孙中山，说是你们讲讲吧，什么主张，他们就冲着国父大大咧咧说。民国的有志青年见了大人物，心里脸上，没遮拦。五四那天，张国焘为首的学生队伍准备前往天安门，校长蔡元培出面劝说，给小张跑上来一把档开，领着队伍就出校门了。<br />
抗战之际，群情滔滔，也是蔡元培出面申说政府万难，结果学生竟然拥上去拖着打。蔡先生是怎样的资格与人格？经此一事，身心伤。<br />
&nbsp; &nbsp; 清末民初，中国民间冒死犯禁的猛人太多了，成了要命的基因遗传，４９年后，遗传错位了。林昭，５７年阳谋初起，没她的事，实在因为看不过所谓右派同学被围攻，忽然她就跳上桌面，大声喝断，和那些围攻者激辩，还当场念古诗。你想想，一个苏州的女子，二十几岁，浑身是民国的刚烈，她的上代就有民国的烈士，而她后来果真拿命抵了自己这股气。她在狱中也有柔弱愁惨之时，留有诗文，言辞凄然，情同秋瑾姑娘――共和国时期多少不安分的少年，包括部分红卫兵，都以为是在继承先烈遗志，都有一脑们子被灌输的革命记忆，谁也不会想到那是民国记忆，他们仿效崇敬的中共烈士，是民国范儿啊。<br />
那年《色戒》播映，我遇见余光中夫妇，余夫人说，我们民国的女子是有烈性的。《色戒》那位烈女子的上代，也是烈士，和林昭一样，一门之中，两代人喋血成仁。<br />
&nbsp; &nbsp; 现在的七零后八零后总算摆脱这致命的记忆了。掐断历史是要动刀的。张志新喉管给切了，但你知道林昭的待遇吗：她在单人囚禁时整天叫骂，狱卒专门制作一个头套，封住她的嘴脸，吃饭时解开，饭后再给严严实实套上，睡觉时也戴着。指挥家陆洪恩当庭叫骂，直接把他的嘴撕了，去刑场路中再给击落下腭，发不出声。切喉管是医学进步，并不止张志新，１９７９年我看过官方报道，总共四十多人犯被切割，其中包括文革后执行死刑的人。<br />
<br />
<br />
民国若是个时间定义，从1912年开始到1949年就结束了。若是个空间定义，它气息未绝。您是如何定义民国的？<br />
<br />
陈：民国那股气，不是民国才有。清灭了，但是清朝上朔整个古代的那种士子气，那股饱满的民风，其实都在，都顺到民国来了。民国是新朝，是古老国家的庞大转型，民气格外强旺，不然哪来那么多前仆后继的乱党和烈士。关于清末民初的叙述，解放后弄得只剩鲁迅一个文本：在他的小说里，那个时代暮气沉沉，老朽不堪，可是你想想清末革命党那份嚣张、那份咄咄逼人，康、梁，还有徐锡麟、谭嗣同，舍我其谁，敢作敢当，是个腐朽时代的征象么？鲁迅自己，说话之猛，诅咒之毒，又岂是孱弱的国民所能为。他曾形容神州大地是“无声的中国”，其实在他的时代，中国吵闹得很哩。１９１５年胡适回国后，上下古今发议论，才二十六七岁，成名后每周择一日，家门敞开，各色人等进去和他摆龙门阵。今之网友或许讥为作秀，可今人哪来那股阳气。现在二十六七岁的博士生留学生，也就整天缠绕论文格式，排列关键词。<br />
民国作为国体，是短命的，粗糙的，未完成的，是被革命与战祸持续中断的褴缕过程，然而唯其短暂，这才可观。一个现代国家现代文明的大致框架，就是那不到三十年间奠定的，岂可小看。单说民国的大学教育，今时休想望其项背，当年浙江的中小学教师是李叔同丰子恺叶圣陶，绍兴镇的中学校长，会是周树人。近时读出版业巨子张元济往事，他好像是前清的举人吧，49年新政权催其北上共与国事，老先生既疑且惧，几度上书推却，用的是汉赋的辞令……民国是丰富的，是古典文化大规模转换的国家景观，回首前瞻，与传统、与世界，两不隔绝。只可惜民国的整体风范，民国的集体人格，才告确立，才有模样，就中止了，改道了，无可挽回。<br />
&nbsp; &nbsp; 民国的前因，是在清代――清晚期，所谓白话文，所谓现代传媒、现代教育、现代习俗、现代价值观，包括初期工业、交通、邮政、商业等等，都出现了――民国的后果，则延伸到１９４９年之后。气数断绝，那要到文革了。毛泽东说：文革是共产党对国民党，无产阶级对资产阶级的继续斗争。我小孩子听着，吓得出汗，我们不是戴着红领巾天天升旗举手要接班吗，怎么还没斗完，现在想想，他很清楚，４９年前认识他、了解他，与他平视平坐的许许多多老辈，都还活着哪。<br />
&nbsp; &nbsp; 我是到了九十年代回国一看，才回过神来：我小时候，从五十年代到七十年代，甚至部分八十年代，满大街是民国人（十三届三中全会主席台上，以邓小平为首的第二代领导人，包括彭真李先念杨尚昆等八老，哪个不是民国人?）很简单，我辈的家长，民国青年，我辈的中小学老师，还是民国青年。为什么大家怀念八十年代恢复高考后的大学教育？原因固然很多，关键一条，那时各大学主要教授都是民国的文人。我上美院时，左翼老前辈如鲁迅的学生江丰，为聂耳填词的许幸之，都还在食堂打饭吃，北京城里，梁漱冥钱钟书沈从文杨宪益，都好好活着呢。现在毛主席应该放心了：他的同代人都死了。<br />
要说空间定义，除了殖民时期建筑和古代建筑，全国目前可看经看的楼宇殿堂，譬如清华、燕京(即今之北大)、北师大，南京的中央大学(即今之复名的东南大学)，武汉大学，中山大学，当然，还有中山陵，全是民国人设计的(部分是洋人设计)。这些年有钱了，中国的大学建筑张牙舞爪，不伦不类，哪像是斯文之地。<br />
&nbsp; &nbsp; 再者，话语算不算空间?所谓白话文，现在公认最好的白话写作是在民国，而民国上乘的白话文是清末旧白话，渊源上溯宋明。４９年后，尤其是７９年迄今的白话文，白是白了，然而无文，眼下你举得出一位文体家么？这是大题目，还得另说。<br />
<br />
<br />
听说您收藏有老照片，有名人如蔡元培的，也有普通的如上海棚屋女人的。能否给我们讲讲，看图说话一下。<br />
<br />
陈:我有一件原版照片，是蔡元培和眷属站在那里，穿着呢大衣，边上站着鲁迅要好的浙江老同乡许寿裳，一副忠厚相，介于旧时乡村读书人和到外面做事后的现代草莽气。我小时候家里的浙江亲戚就是那模样，他俩是因为德国日本留过学，自有一种沉稳豁达，不可言传。<br />
&nbsp; &nbsp; 另一幅照片是美国记者１９４９年拍的上海苏州河边棚户人家的中年妇女――上海没有比住河边棚户更底层的人了，小学里有钱孩子骂人，就说他家是棚户区――可你瞧那女子，干干净净，头发用水油蓖过，梳得一丝不苟，很好看的发髻，双手正在衣襟扣那斜到腋窝的盘扣，给太阳照得眯眼，面容饱满好看，甚至有点富泰，一点不贱、不自卑。现在瞧见农民工和城管队动辄殴打的盲流，我就想起小时候邻居穷人家规矩，平时出来头发梳梳好，衣服整齐，干净见人。真的，那位棚户女子立即叫她扮演宋庆龄，虽不合格，也比《建国大业》里的国母更对。不是演员不好，是没见过民国妇人的起坐言谈，即便装扮都到位，还是没感觉。我记得直到文革前，随便哪个贩夫走卒都是有模有样，小职员之类，头发中间分条头路，像周立波那样梳得精光。文革开始人斗人，全疯了，那才叫斯文扫地，不顾颜面――从此中国人的模样，江河日下，不可收拾了。<br />
&nbsp; &nbsp; 民国时代的商人、教授、文员、流氓、工农、女性，甚至儿童……各有范儿，山东出版的《老照片》提供了丰富的影像资料，大致是民初到三十年代，民国风最鲜明，四十到六十年代进入模糊期，形神扮相多有重合，文革到八十年代，民国气完全消失，代之以共和国类型。我听美国学者说，文化人类学家曾用人物影像作世界范围各区域调查，以百年为跨度，研究现代化过程中人的面相、精神和气质，据说中国人的脸百年变异最巨大，最深刻，前后难以辨认衔接。可惜我无法找到这珍贵的资料。<br />
再说风光，去年我得到一堆珍贵的旧版相册，一是英国人法国人二十年代前后拍摄的北国与江南，看得我心痛。那时的中国虽已内战频仍，然而只看景观，真是富饶宁静的古国，和今之欧洲没有两样，即便贫瘠落后之地，屋舍俨然，仟陌纵横，穷归穷，然而干净、自为，没有败坏，处处编织在中国自己的美学图画中。<br />
&nbsp; &nbsp; 另一组影像是日本战时出版的系列战争摄影，记录日军攻占各省市的军事景观，我在每幅照片的背景中看见了当年中国各大省区和都市，各地风格迥异，锦绣河山，非常非常好看啊，和今日景观全然不同。然而这组照片尤其令人沉痛而气短，不说也罢。<br />
<br />
<br />
如何看待民国留下的遗产？我想这既是生活方式的，也是文化的。<br />
<br />
陈：共产党就是一份庞大的民国遗产。你能想象我党会在晚清，或者１９４９年后这才光荣诞生，发展壮大吗？<br />
<br />
<br />
人们总感叹，看那些民国老照片，觉得民国人活得比现在人挺拔、时髦、有教养、有威仪――哪些东西是我们丧失掉的？<br />
<br />
陈：民国人什么罪都受过，战争、逃难、饥荒、沦陷、破产、亏空…上海老辈说起，顶屈辱是过外白渡桥要给镇守的日本兵鞠躬，搜身，吃耳刮子。我父母在抗战逃难中亲眼见过被轰炸后狼籍道旁的尸体……可是民国百姓从来不知道什么城乡户口、待遇级别、粮票油票、五类分子……更没有经历过上级下级之间，同学同事之间，街坊邻居之间，甚至家人与爱人之间的检举揭发，彼此防范，划清界限，断绝关系之类，即便老于世故的民国人，也不知道做人还有检讨、认罪、批斗、下放等等等等花样。我父亲回忆，说是１９５０年潘汉年给全市职员做报告，长达七个钟头，叫做“放下包袱”，意思是你解放前干过什么，全部交代，重新做人――同期，周恩来在北京大学也做同样的报告，也是一讲七个钟头，要所有民国书生从实招来――这一套，民国人哪里领教过?那时人老实啊，于是全班加入三青团之类，据实写出，签了名，交上去，以为可以效命新中国了，哪晓得从此不得好活，不得好死，牵连亲友，祸延子孙……<br />
　　那报告做了没几年，潘汉年同志自己也给铐起来，关进大牢了。<br />
&nbsp; &nbsp; 简单说，民国人没有大规模被侮辱与彼此侮辱、被监管与彼此监管的集体经验。你看抗战时期那些流亡西南的师生教授们，一路千辛万苦，稍稍安定了，长衫西装箱子里取出来，穿穿好，拍出照片，斯斯文文，有尊严，有气象，一点看不出怨恨愁惨。你从史料看，他们之间有派系，有恩怨，有各种难堪，但没有长期被侮辱被贬损，因此戒惧而扭曲的集体心理。<br />
&nbsp; &nbsp;《老照片》里许多坦然自若的男子女子，不过是当年乡镇打工仔，同样的角色搁在今天，面目卑贱萎缩。民国虽说还没消灭阶级，士农工商的关系绝对不像教科书说的那样。有次我去天津参加什么企业发放助学金给中学生的典礼，台上领导轮番发言，肉麻夸张，好像都是活菩萨。只有位老教授说话平实，说他抗战时家里穷，全靠民间资助才读完中学，你想，沦陷时期还有资本家设立慈善机构，不事声张，很朴实：小孩子拿份成绩单，说说家里怎样穷，不必填表申请，不必感谢党，每学期自去领钱就是。<br />
<br />
<br />
民国是离我们最近的一段“大历史”，大师辈出、精英涌现，如何看待民国的杰出人物?我简短列一个单子，想听听你对他们的言说，如章太炎、蔡元培、陈寅恪、梁漱溟、梅兰芳、徐志摩、闻一多、鲁迅、张爱玲、赛金花、梁启超、张伯驹等。<br />
<br />
陈：我家弄堂里有位白面书生，兰布中山装，相貌清正，玉树临风，开口说话清清楚楚，终日和一帮野兽般的小混蛋周旋着，是静安区一所民办小学的老师，我上学时天天看见他。数年前他读到我的哪本书――真是不好意思――写了几句评语，别人转告我，才知他是章太炎先生的孙子，因为出身不好，六十年代给塞在弄堂的民办小学里，可他一年到头像个君子，好有教养。八九十年代他被起用了，是上海政协的咨议员吧。前几年我去拜访他，得到他写的书。<br />
&nbsp; &nbsp; 我至今记得他斯斯文文站在弄堂口，又正派又礼貌，对一位调皮透顶冲出队伍的小男孩说：“听见吗？！回来！你给我回来好不好？”那小子理都不理他。<br />
&nbsp; &nbsp; 民国人对人对己，有礼貌，有规矩。文革批斗老年人，再怎样挨打挨唾沫，跪下去，拎起来，论到说话了，清清楚楚，凛然有自尊――他们不知道怎样说假话，说软话，他们还没学会共和国的语言。包括被批斗的延安时期老党员，摁下去了，一顿暴打，好不容易给扯着头发抬起身，也不过是说：唉呀！小同志，你们不懂历史啊！<br />
&nbsp; &nbsp; 于是劈头盖脑接着打。<br />
&nbsp; &nbsp; 章太炎、梁启超、鲁迅、蔡元培、陈寅恪、梁淑冥……我并没有合适的资格和语言评说他们。这类动物绝种了。我们的时代固然还有许多聪明有才之人，但所谓“人物”不是指聪明和才学。单说才学，也无奈。譬如张爱玲，不提小说，她的古文和英文，会把《海上花》全部理过，还用英文写影评、写小说《雷峰塔》和《易经》。这不过是她的小动作，及今也没哪位中国作家弄得来：她上过北大复旦中文系吗？她交过哪篇论文？<br />
&nbsp; &nbsp; 现在昏天黑地闹什么英文考试，想得到吗，方志敏这样的大烈士，你以为就会谋反吗？他在江西小地方上的是教会学校，十九岁前后就用英文写小说，发表在地方刊物上：当年的地方刊物，居然有中国人书写的英文小说！中共元老张闻天，还翻译过尼采。又譬如老左派周杨，整人无数，可他参与翻译的《安娜&#8226;卡列尼娜》，圆润谨严，今天的译本哪里比得，他也没上过什么外语学院，更不是教授博导，年纪轻轻就在上海滩支使党羽，逼攻鲁迅了。现在你拎个学生会小党棍出来让他动手翻译翻译?！<br />
梅兰芳也实在没办法：如今哪个中国大牌演员出访美国，出访俄罗斯，也弄不出他那时的动静。从影像资料看，他与洋人交接不过是微微笑着，微微欠身，斯文得不得了。他的优越是因民国初起，古中国文艺第一次亮出国门，世界瞧着新奇，而他也果然金贵，艺术与生活，宛然一体。见过他北京四合院老照片吗，如今哪位身价千万的角儿有那样的家。<br />
&nbsp; &nbsp; 赛金花，不知如何置评。这类女子在我们的时代根本没有。自然，改革开放后嫁给洋人的女子多得很，挑头几位都有一本经：星星画展女画家李爽１９７８年前后和法国人相好，愣给关监狱两年，惊动法国总统，和邓小平讲了，兼有一帮法国书生举牌抗议，这边才放人运到巴黎去―－无论事因，古中国与异族间的交际与通婚，源远流长，李爽这种事告诉赛金花德瓦西，人家根本不知怎么反应啊。<br />
我们谈民国，只能谈著名人物，太多平民故事被淹没了，无从谈起。去年读龙应台先生《大江大海１９４９》，许多动人的小故事。内战起来，中原数千名中学生由老师带着，浩浩荡荡往南逃，每宿一地，就在檐下廊外就地上课。实在太苦太险，中途不少孩子失踪了，离开了，其中有位湖北女孩临别送一册《古文观止》给她相熟的男同学。此后这一册书居然成了逃难学生唯一的中文教科书，一路用到缅甸的法军集中营，用到台湾。可惊可敬啊：那位男同学五十年后回到湖北，找到书主，完璧归赵，俩老头老太捧着破书，拍了一张照。<br />
&nbsp; &nbsp; 他们算民国精英吗。一本《古文观止》，也不是为了研究国学。<br />
<br />
<br />
几座有民国气息的城市：北京、上海、天津、南京、台北。您作何评价?<br />
<br />
陈:都拆了，评价什么?上海是因为要留着产业，上交利润，兼以上海殖民化形制相对透彻完整，所以没大动――１９９２年以后不客气了，开始动手，面目全非――１９４９年后给糟蹋给冷落的好城市，就是说，民国年间已经相当规模实现西化现代化的中国城市，是天津、武汉、南京。现在老区老街老建筑，留是留着一些，局部可看，谈不上民国气息了。你读杨宪益那代人的回忆，大约可以想见吧。我有幸认识杨宪益的妹妹，看过老太太家过去的照片，多好的一座城。<br />
　　台北也没有民国气了，倒不因为这边，实在那是一座日据时代的早期现代城市，和英美法德人下过心血的上海天津，原不能比，国民政府过去后老想着反攻大陆，没怎么建设。真的弄起来，是九十年代后了，但你进入台北的人家，可看耐看的空间，可就多了。台北和民国人记忆中的民国，难比较。白先勇小说中的那些太太们初到台北，想煞南京与上海。<br />
<br />
<br />
说到民国的可爱之处，您会怎么说？<br />
<br />
陈:以我们的教育，民国的一切都是“旧社会”，这是大误解。相对人人梳辫子的大清，民国才是不折不扣的新中国。胡兰成的说法比较可喜而平实，他说：民国好比是“新做人家”，凡事初定，气象清新。你看所有民国老照片，虽是黑白的，陈旧的，沧桑岁月，可是细看进去，一本正经的天真淳朴。抗战时期知识妇女们笑嘻嘻扛着大刀，天真得可笑可怜，不过是拍张照。你看阮玲玉周旋那份嗲，那份柔弱，也属憨态可掬，哭着哭着，又笑起来。民国男女的婚恋聚散，也是一绝，动不动登报发启示。跑去延安的不少青年，动因是为逃婚，你看延安时期黑白记录片，一帮女青年排队拉手团团转，跳舞唱歌，也是质朴未凿，一派天真，又像发嗲又发狠，那是天津南京女子大学带过去的西洋集体舞……要不是后来知道延安整风整死人，要不是许多延安男女后来成了右派，发放东北西北，死得不明不白，那延安十三年真是天下最纯真的生活、最浪漫的地方，西方左派看了吃不消，感动死了。这种纯真浪漫，只在民国，迄今往后，中国再也不会有，也不可能有了。<br />
<br />
<br />
对于民国时期的文艺，您的整体判断是什么?<br />
<br />
陈：民国虽有检查机关，但没有中宣部、文化部、广电部、文联、画院、美协、舞协、音协、剧协……他们管制媒体的方式，譬如雇些打手砸你的书店出版社，真是小儿科，也属天真幼稚型。他来砸，说明你和他是分开的，现在用的着砸吗？张道藩，徐悲鸿的留法同学，抗战时期出任国民党主管文艺工作的大官，略微相当于中宣部长吧，他晚岁追求徐悲鸿前妻蒋碧薇，写情诗，其中有句：“我身上一块块肉割下来，每一块写着我爱你!”肉麻吗？是的，所以这等官员怎能是共产党的对手。<br />
前面说了，民国文艺多是民间生发的――我母亲唱过几句《总理遗嘱》给我听，那种志气的表达，好谦虚，像是哪位乡村教师业余写出来――其中左翼的，或者说，偏左的文艺（即相当于今日“体制外”文艺），是最精彩的部分。它与1949以后的文艺有关系，但又没关系，这是大话题，也得另说。<br />
<br />
<br />
是否可以说，我们现在的认知系统，除了科技和物质在进步之外，其它方面一直在“退步”?这就涉指到一个重要的命题：我们为什么怀念民国?<br />
<br />
陈:我不愿说是退步。今天大陆做成的种种事，至少，论硬件，是民国一代想做的事情。国民政府不剿共、不抗战，也得实现“四个现代化”。百年中国的一切折腾，都是为强国。我们为什么怀念民国――虽然我不知道究竟多少人果然怀念民国――大概因为中国总算比较地强了，阔气了，忽然发现人的状况不妙了，时代的滋味不妙了，回头看看，居然乱世民国还有那么多妙事，那么多奇人，所以怀念吧？我不知道。顾念前朝，是历代中国的士夫情结，如今的怀念民国，性质又似不同。你们为什么要做这一期“民国范儿”专题呢?<br />
&nbsp; &nbsp; 政治问题归政治问题，我愿有保留地肯定今天的中国。若说民国的一切都比今天好，那是荒谬的。那时的中国还是前现代国家，像样的公路没几条。民国政治的幼稚和败笔，不知有多少：国民政府曾经明令废除中医；吴佩孚时代，议会居然集体讨论拆毁故宫，将紫禁城辟为政府机关，所幸吴大帅电令制止，违者“格杀勿论”；49年后，拆毁故宫的设想并未搁置，拖到六十年代不实行，只因国家太穷，又闹文革，顾不上，不然天安门以北全是办公楼了。所以我们时代的种种人祸，论前因，部分缘自民国时期泛滥无忌的文化激进主义。<br />
&nbsp; &nbsp; 民国离乱的一代，渴望富强。问题是途径与代价。现在是谈论代价的时候吗？我们还在付代价，还不知要付多大的代价、付什么代价呢。<br />
<br />
<br />
现在颇多民国研究，如南京大学有《民国研究》杂志，北师大修订了《民国史料丛刊》等，民间对民国研究的热情更高。对于民国研究热，您的意见是什么。<br />
<br />
陈：我感激民国研究学者。去年读杨奎松先生写的书，平实有据，非常好，我读到历史的理性。那是政治研究，不算文化研究，看你怎么读。在一切民国史料的缝隙里，我随处窥见那个时代的气息和质感。<br />
<br />
<br />
不慕今人慕古人。如果说真有“民国范儿”这样一种生活礼仪或生活方式，“范儿”针对“失范”而言，我们现在的“失范”又是什么?<br />
<br />
陈：别说“民国范儿”，连五六十年代、七八十年代的种种“范儿”，也都遗失干净了。不是吗？如今但凡可观而存有价值的类型，几乎都“失范”了：我们真失得起啊。你想想，七八十年后的中国人会不会无限向往地说：啊！“改革开放范儿”太迷人了！真有意思啊――你举得出哪一群当今人物，日后亮得出去，留得长久，居然成为后人怀想追慕的“范儿”吗？ <br />
<br />
<br />
从民国里来，到民国里去。那些出生于民国的人，也都是民国的遗老遗少了，但却回不到民国里去。您怎么看待他们。<br />
<br />
陈：木心先生曾经笑说这一层，警句：“遗老不够老，遗少不够遗！”如今哪有民国的遗老遗少啊：你去瞧瞧国共两党的儿孙辈。<br />
&nbsp; &nbsp; 不过我倒不以为“回到民国”是一种价值。三十年来，中国被有限释放的活力即便从未以民国为指归，但是民间各种自发的个人行为个人价值，正在各层面破茧而出，呈现奇怪而惊人的姿态，成为社会潜流，有如方兴未艾的网络。无数青苗：八零后、九零后，虽说还在念政治考试的经，但他（她）们和民国年间的孩子们理应等同，朝向未知的可能性。为什么我不愿苛责年轻人？清末民初的孩子也曾被世人讥为“一代不如一代”，然而一代送走一代，今天，中国到底不是清末民初烽火离乱的中国，也不是文革时期的中国了。<br />
&nbsp; &nbsp; 我有时瞧见簇新的青年一脸无辜站那里，即便空白如纸，总算不见党气戾气了。你知道吗，最近看江苏台为男女生牵线搭桥的电视专栏《非诚勿扰》，我非常感动。不为男女情，而是看见了坦然率真的新青年，农民工组合尤其可爱。每位男女公开说出自己的好恶，言语得体，态度自然，虽有位党校女官安插着（烫头发、抹口红，颇有几分性感），但是节目的气象实在是真实的，人性的，如胡兰成所说，有一股对人世的相信――民国的可爱，不就是这真切与相信吗？<br />
<br />
<br />
最近的历史又是最远的历史，因为历史越近越难看得真切、辨得清楚。说了这么多民国的话题，可我却疑心它是否真的那么美好。<br />
<br />
陈:有一位早期去延安，后来走掉的中共史家，名叫司马璐。他的自传详细描述出离延安后的四十年代中晚期，期间他去了重庆、南京、上海，又办报纸，又组党。他说，他不知道在中国还有比那个阶段更自由的感觉。当然，这家伙是个叛徒，是反动派，虽然文革后的北京党史专家据说经常越过海外向他请教当年的人物与故实。<br />
&nbsp; &nbsp; 自由是什么?自由是指叫嚷自由、追寻自由的人。民国时期的共产党人莫不公然宣称民主和自由。如今我们大概真的自由了，不叫，不想叫，也不许叫――九十年代末李慎之先生冒险破这自由的题，人劝他慎重一点，他大声说：“由我来先说吧，我是党员！”李先生是勇敢的，问题是，他本来是自由的：四十年代他有投奔异党的自由，也为了自由而加入共产党；过了整整五十年，因为“我是党员”，他才能重提当初共产党挂在嘴上的自由：他晚年终于明白，不分资产阶级无产阶级，自由就是自由。这一层，他不如五十年前的司马璐，尤不如有言在先的储安平。储安平1948年就说：在国民党那里，自由是多与少的问题，今后可就是有和无的问题了。<br />
&nbsp; &nbsp; 半个多世纪过去。民国的种种善，民国的礼义廉耻信，早被大规模玷污，大规模失传了，民国的种种恶――贪污腐化、裙带关系、横征暴敛、弱肉强食、丧尽天良――倒是进步神速，以至发扬光大：不是吗，论恶，论恶的丰富性、离奇感、创造性，我们绝对有资格看不起民国人。　<br />
&nbsp; &nbsp; 是的。中国是以这艰难的进步警告中国：中国的进步何其艰难，何以艰难――民国是否真的那么美好？好问题。我非常希望你是对的，也希望我以上的意思全属错谬：为了免于沮丧，人乐意肯定自己存活的年代；为了免于虚妄，人会质疑传说中的前朝。遗忘民国吧，它早已被唾弃了。不过，你所说的“美好”，或不美好，是指什么呢? <br />
　　　　　　　　　　　　　　　　　　　<br />
<br />
<br />
２０１０年７月２８日写在北京</div>

							
							
							
							
