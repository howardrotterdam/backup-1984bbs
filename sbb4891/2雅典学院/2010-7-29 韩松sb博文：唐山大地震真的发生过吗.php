																											<div id="postmessage_764710" class="t_msgfont">科幻作家韩松<br />
<br />
1、履历<br />
<br />
　　笔　名： 小寒、小青、金小京<br />
　　性　别： 男<br />
　　出生年月： 1965年8月<br />
　　民　族： 汉族<br />
　　生于重庆。1984～1991年就读于武汉大学英文系、新闻系，获文学学士学位及法学硕士学位。1991年，他以优异的考试成绩进入新华社，历任记者、《t望东方周刊》杂志副总编、执行总编，对外部副主任兼中央新闻采访中心副主任等职。在此期间，他撰写了大量报道中国文化和社会动态的新闻和专访，还参加过中国第一次神农架野人考察。和有关克隆技术进展的报告文学《人造人》。<br />
2、获奖及贡献<br />
<br />
　　韩松的作品极富文学情趣，结构精巧，内蕴深远，可谓独树一帜。1988年、1990年获中国科幻银河奖，1991年获世界华人科幻艺术奖，1995年获中国科幻文艺奖。其大部分短篇收录在《宇宙墓碑》中。代表作有中短篇小说集《宇宙墓碑》、长篇小说《2066之西行漫记》、《让我们一起寻找外星人》、《红色海洋》等。<br />
　　韩松的科幻文学创作起源于大学时代。韩松的早期代表作，是获得了台湾《幻象》杂志主办的“全球华人科幻小说征文”大奖的《宇宙墓碑》。接下来，他的小说还获得了包括《科幻世界》“银河奖”等多种科幻奖项。他的作品别具一格，在读者中产生了广泛的影响。包括郑文光、金涛、刘慈欣、姜云生在内的许多中国著名科幻作家都曾表示，韩松在中国科幻文学中具有不可取代的特殊地位。美国《新闻周刊》、科幻评论刊物《轨迹》、英国科幻研究刊物《基地》等，都曾有文章谈论过韩松科幻文学的成就，并给予过高度评价。<br />
　　思考和写作是韩松的最大乐趣。他的创作不局限在新闻报道和科幻小说。比如，他也创作过非常独特的诗歌、杂文和无法归类的其他小说。1999年出版的《想象力宣言》，至今仍然是中国科幻文学领域最富于批判力的理论著作。《鬼的现场调查》则是集生动曲折的考察记述、引人入胜的文史考证和瑰丽的科学幻想于一体的报告文学。类似的报告文学作品还有展现克隆技术进展的《人造人》等。参与撰写的政论著作《妖魔化中国的背后》风靡一时，“妖魔化”一词成为公共词汇。<br />
　　像许多文学大家一样，韩松从来不在乎作品的发表与否，更不注重个人名望。每日的写作本身，就是他的享受之源。也正是这种对创作的执着和热情，成就了他对科幻文学范式的多次超越。而《红色海洋》则是这种文学超越的最新尝试。<br />
