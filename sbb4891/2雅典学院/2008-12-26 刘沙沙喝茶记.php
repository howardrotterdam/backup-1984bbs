																											<div id="postmessage_14280" class="t_msgfont">转载<br />
<br />
【百草止水注】刘沙沙是天涯社区知名网友，以下是她被请去喝茶的经历，可以说非常精彩。特转帖于此，希望仁者能够见仁，智者能够见智。正文里的第一人称皆为“刘沙沙”，大家可别搞混，呵呵……<br />
<br />
【正文】<br />
<br />
（一）<br />
<br />
１１月２０日下午五点左右，我去南京师大，给郭泉媳妇李晶送去一份慰问礼物。东西放在门口传达室。然后回夫子庙，上网，吃饭。九点左右回旅馆，刚到总台，身后就跟过来一个小眼睛中年男子，“你是刘＊＊（我真名）吧？”我说是，他说“我们是南京公安局的，请你和我们走一趟。”我说行，但我得先打个 [url=]电话[/url]。他们说不行，不能打，手机得关机。我看他们是好几个人，还有人坐在旅馆大堂一边的沙发上，就也走到沙发上坐下，笑着说，我正好也想找你们，交流一下意见呢。他们也笑，问，什么时候到的南京，我说今天早晨，他们又问，到南京干什么来了？我就笑了，说“看郭泉来了。咱们直奔主题，不弯绕，我就是看郭泉来了。”然后我和警察一行，上楼去房间，把零碎东西都收进背包里――我放在桌上的零钱硬币太多，我不想要了，结果还是一个警察替我收进包的。这点可见他们态度还不错。<br />
<br />
上了车，我是坐在后排，一左一右两个警察。车开动，他们就在车里问我：“你为什么要来看郭泉？”<br />
<br />
我吭吃了一下，在谈一些太高尚的事物之前，我总是会这么吭吃一下，不好意思一下：“如果我说是为了――理想，你们会怎么看？但我确实是为了，理想，民主理想。我认为中国民主改革，下一步肯定要走多党制的道路，而郭泉建党，是先走了这一步。总之我来，就是为了，理想。”<br />
<br />
车内静默几秒，估计他们也在感叹，在直抽冷气：这年头，理想――<br />
<br />
几秒钟后，警察：“你认识郭泉？”<br />
<br />
我说“不认识，就是在ＱＱ群里见到郭泉发的文章，新民党党纲什么的。”<br />
<br />
警察：“那么你是仰慕他？[url=]崇拜[/url]他？”<br />
<br />
我一听就乐了：“我不仰慕任何人，不崇拜任何人，我看政治人物一概是平等的看待。我只是看了他的党纲，赞同他的党纲里的一些原则。比如多党竞选，轮流执政什么的。当然我也不赞同郭泉的一些做法、说法。比如他砸汪直墓碑，我就觉得很傻，二愣子。再比如那个一千万党员的说法，很不严谨。这是我不赞同他的地方。 ”<br />
<br />
又是几秒钟静默。<br />
<br />
我打破沉默：“其实我今天上午就想找你们的，想找你们交流一下看法，关于郭泉，以及关于你们的工作方法改进什么的。”<br />
<br />
警察：“那为什么又没来呢？”<br />
<br />
我嗑巴一下：“嗯，还是有点害怕吧。”<br />
<br />
开车的警察就轻轻冷笑了一声。<br />
<br />
到了某派出所（后来知道是南京市鼓楼区华侨路派出所），走到一楼楼道尽头，过一道铁门，上楼，再在二楼楼道里走过了七八个办公室，他们打开一间办公室：“请进。”<br />
<br />
我站在办公室门口，头一昂，眉毛一抬，欣喜地笑了一下，特振奋！特期待！啊，终于轮到我们这一代人，站到革命的舞台上了！<br />
<br />
但我没想到，坐下之后，光是那个温和的小老头警察一边问一边在电脑上打入我的基本情况，身份住址工作什么的，就琐琐碎碎弄了好久，把我的豪情都给磨碎了，思路也给打断了――这是一个教训，不要一进派出所就提起十二万份的斗志精神，还有一大堆琐碎在等着你呢。哦，那个温和的小老头警察，我给他起外号叫“尖下巴”。<br />
<br />
等到基本情况填完，他和颜悦色地问我，什么时候到的南京，我说今天早上，然后突然旁边一个白净方脸警察，站着，居高临下，很凶狠地：“谁让你来的？”我一口反击了回去：“我自己来的。”他又逼问一句：“到底谁让你来的！”我一口咬定：“我自己来的，没谁让我来，没人能‘让’得动我来！”<br />
<br />
啊对了，这个白净方脸警察，我给他的外号是“郭粉”――以后大家能看到，他对郭泉的钦佩仰慕之情。<br />
<br />
然后那个温和的中年警察，尖下巴，看要僵，就在旁边和颜悦色地岔开了：“你认识郭泉？”<br />
我：“不认识。”<br />
“那你怎么知道郭泉的？”<br />
“ＱＱ群里。”――这里，警察表现出了另外一个烦人的地方：同样的问题，问Ｎ遍。一个人问了，换个人再问一次，今天问了，明天换个人再问一次，目的就是让你疲倦，也核对一下前后有没有出入。当然，也因为他们没啥词儿。<br />
<br />
然后我就又讲了一遍：在ＱＱ群里见到的郭泉文章，党纲，我赞同的地方，不赞同的地方：赞同他的多党制，民主竞选，不赞同他的一千万党员的说法，不严谨。等等。他们个个点头称是，我说一句，他们嗯一句，气氛显得很温和，很交流。<br />
<br />
在很温和的气氛中有人问“宁文忠你认识吧？”<br />
我说认识。<br />
“他的网名是什么？”<br />
“砍樵人。”<br />
“你怎么有他电话的？”<br />
“他在ＱＱ群里留的。”<br />
“什么时候留的？”<br />
这时我突然反感和警惕了，我觉得这已经不是温和的交流意见了，而是在套口供了。马上绷上嘴，不说话了。（刘沙在没有经验的情况下说出朋友，仅此一次，仅此几句，再往后，他们再也问不出了。）<br />
<br />
（这次喝茶，我最困难的是第一天，２０号晚上。因为没有经验，也因为心态太宽松，结果，上了他们不少当―――你想人家是朋友，人家想你是犯人。你想的是交流，人家想的是审问。我本将心做明月，谁知明月照污水沟！―――）<br />
<br />
（二）<br />
<br />
这时又来一个警察，坐在我右侧后方发问，我扭脸一看，此人尖长脸，单眼皮，黄青脸色，满脸疙瘩，看着很丑很凶。我给他起的外号是“中统特务”，至于为什么叫这个外号，往下看大家就明白了。<br />
<br />
下边这一段，是刘沙最出糗的一段。首先他是坐在我侧后方发问，你要想尊重他，就必须不停的扭脸看他，这样就很累，很分散注意力。其次，他语速极快，又急又快的问题一句接一句，你前一句没答完，他后一句又来了。一句没完，“啪”又一句，一句没完，“啪”又一句，句句都是隐含了前提的“阴险问句”，刘沙没经验，技不如人，又被前边的几个警察搞累了，这会儿来不及分辨他问句中的“阴险前提”，发现被套已经晚了。输了。确确实实技不如人。输了。心服口服。<br />
<br />
下边这段对话语速极快，大家可以想象这种语速极快的对话节奏。网警同志也可以找南京方面核对一下谈话录音，学学经验――　　~_~<br />
<br />
很快的对话，而且，几乎每一句，我还在说最后几个字的时候他下一个问题就杀来了，我怀疑这家伙是特别训练过的，只听你一半句子就知道了整个意思，就开始了下一句！<br />
<br />
“你到南京来干什么来了？”<br />
“来看郭泉来了。”<br />
“你怎么知道郭泉被抓了？”<br />
“在ＱＱ群里。”<br />
“你为什么来看郭泉？”<br />
“我怕他在里边吃亏。”<br />
“哎你对郭泉比他媳妇对他都关心啊？”<br />
我一下就把脸沉下来了：“你什么意思？”（我知道ＧＣＤ现在学着国民党骂李公朴，开始在这方面造谣了）<br />
我扭脸面对桌子，不看他了，问话停顿了。<br />
<br />
几秒钟后，他又开始了，从头问：“你到南京干什么来了？”<br />
“来看郭泉来了。”<br />
“为什么来看郭泉？”<br />
“怕他吃亏。”<br />
“吃什么亏？”<br />
“在里边挨打。”<br />
“你以为我们会打人？你有什么证据证明我们会打人？”（威胁的口气）<br />
我因为还想给对方留面子，当面说不出口“你们法西斯”这种话，只好犹豫了一下：“……听说过。”<br />
“你以为你见得着郭泉？”<br />
“见不着，只好见他媳妇。”<br />
“见他媳妇干什么？”<br />
“问他吃亏没有。”<br />
“如果他吃亏了，你会怎么办？”<br />
“在网上呼吁，打抱不平。”<br />
“你见着他媳妇没有？”<br />
“没见着。”<br />
“为什么没见着？”<br />
“他媳妇吓得不敢见人了。”<br />
“那你打算怎么办？”<br />
“回去。”<br />
“还发不发呼吁了？”<br />
“不发了。”<br />
“为什么不发了？”<br />
“因为我现在没证据证明你们打人了。”<br />
“那你原来不也是没证据，现在不也是没证据吗？你不前后矛盾了吗？你不扯蛋吗？”<br />
我一听扯蛋，一下子就恼了，把手里的纸杯往桌上一墩，脸一绷，不说话了。<br />
<br />
――大家看清楚，我输在哪儿了吗？<br />
<br />
他很得意地站起来了，站到我侧面了，而我，几秒种之后我长长地哦――了一声：“哦！――我明白了，你的每一个问句，都是一个隐含了前提的复杂问句，我回答是或不是，都上当了！――在正常的司法中，是不准使用复杂问句的，用了就是诱供！”这时，我对面的方脸警察，和中统特务，一下交换了一个钦佩的眼神，那真是，惊喜，钦佩的眼神，喜悦的眼神！棋逢对手的眼神！<br />
<br />
然后中统特务，站着，嘟嘟嘟嘟又说了一遍“你矛盾”，我一下打断了：“你说我扯蛋，你对我人身攻击，我不回答你的问题了！”他马上又开始连珠炮似的嘟嘟嘟嘟“你原来没证据，现在不还是没证据么？你这不扯，扯谎么？”<br />
<br />
然后开始发狠，说了Ｎ多话，我因为在回味刚才的失败，没听清他说什么，只听见最后几句：“你一个女人家，管这种事情，你多管闲事！你闲吃萝卜淡操心！”很威胁的、江湖狠辣的口气，倒把我听笑了，仰起脸来，笑了：“你说的几句话我很熟哎――”<br />
<br />
他俩都意外了，都傻傻的听我往下说：“你说的几句话我很熟哎，都是旧电影里边，革命电影里边，中统特务说的哎！”一下把他俩卡住了，半天，互相看看，都给怄笑了！中统特务一边笑一边不服，嘟囔着“什么中统，还军统呢……”　　　~_~<br />
<br />
……<br />
<br />
这时那个尖下巴的温和警察从外边进来了，递给我一张纸要我签字，是一张讯问通知书：<br />
<br />
刘＊＊（我真名），你因为涉嫌煽动、策划非法Ｕ行、Ｓ威而被讯问。<br />
<br />
我盯着那张纸看了好久，对“煽动”“非法”二词很是反感。但看了半天――“好吧，毕竟只是‘涉嫌’。”就提笔签了字。<br />
<br />
下边这一段话，是警察另一个无赖的地方，逻辑混乱的地方。那是我以一对四，和四个警察混战之际发生的对话，这段对话，在二十一号早晨又重复了一遍。可见他们犯错误都犯得没有新意：<br />
<br />
（中统特务）“你为什么来看郭泉？”<br />
刘沙沙：“因为我赞同多党制，赞同他建党的行动。你们说他颠覆国家政权，他一个书生，没枪没炮没武装，他怎么颠覆国家？”<br />
（中统特务）“你认为我们会无缘无故抓他吗？”（已经开始用公安机关那破碎不堪的信誉来做保，来威胁我了。）<br />
刘沙沙：“那么好，你们给我证据证明郭泉颠覆国家了，我马上可以声明再也不参与郭泉这事了。你们有证据吗？”<br />
（中统特务）“有证据能给你看吗？这是机密！”<br />
刘沙沙：“那对不起，在证明一个公民有罪之前，他就是无罪的。”<br />
（尖下巴）“所以我们说郭泉，也只是涉嫌，涉嫌，调查！”<br />
刘沙沙：“那么好，你们认为他颠覆国家，我们认为他没颠覆国家，我们有权组织Ｕ行Ｓ威，有权做出我们的表达！”<br />
（尖下巴）“你们的表达是错的！”<br />
刘沙沙：“你怎么能说我们的表达是错的？”<br />
（尖下巴）“郭泉是颠覆国家罪，你们为他Ｕ行Ｓ威，你们就是支持颠覆国家！”<br />
刘沙沙：“我们没有！我们就是Ｕ行Ｓ威，属于表达自由！”<br />
…………<br />
大家看清楚了吗？当你直接质问郭泉有什么罪，有什么证据时，他们一是偷换话题，用“你认为我们没证据会抓他吗？”这种无赖反问来抵挡，二是退让一步，说涉嫌，只是涉嫌！<br />
<br />
可，当他想吓唬你“营救郭泉也有罪”时，就把郭泉颠覆国家当做一个已经成立的前提来说了！<br />
<br />
然后又一个警察，站在我另外一个侧后方，问：“你跟法Ｌ功有联系吧？”<br />
我说“没有。”<br />
“为什么？”<br />
“因为我不赞同法Ｌ功的世界观。我是唯物主义者，百分之八十的马克思主义者。”<br />
“百分之八十？”<br />
“对，因为我接受百分之八十的马克思主义。”<br />
“那你不接受的是？”<br />
“马克思的社会主义理论中，对于民主方面――”还没说完就被中统特务打断了：<br />
“你什么时候到南京的？”口气很凶。<br />
我一愣：“不是要我讲民主么？”<br />
“这会儿是讲事实！你什么时候到南京的？”<br />
“今天早上。”<br />
“坐的什么车？”<br />
“火车。”<br />
“几点到的？”<br />
“六点半。”<br />
“哪个车站？”<br />
“玄武湖那个站。”<br />
“到站之后干了什么？”<br />
“吃Ｍ当劳。”<br />
“然后呢？”<br />
“和李晶联系。”<br />
“还和谁联系了？”……<br />
<br />
这时我已经疲倦了，就抗议了：“你们找到我时是十点左右，这会肯定过了十二点了，（登记身份住址什么的太嗦了），这会肯定过了十二点了，你们不让我睡觉，搞疲劳战术，不人道，我抗议你们的不人道行动，我不会再回答你了。”然后就把眼闭上，嘴绷上了。<br />
<br />
这时，那个尖脸的温和警察就又说话了：“你想搞民主，很好。但是中国国情不同，肯定不能照搬西方……”什么什么什么的，一大堆。<br />
<br />
这就是他们又一个可恨的地方，问你最擅长，最愿意讲的民主理论问题，当你想跟他们讲理论时，没说两句他们就打断你让你讲事实，让你的注意力破碎化。当你讲事实被他们问烦了、抵触了，绷上了嘴时，他又跟你讲理论，污辱民主，逗引你的说话欲望――</div>

							
							
							
							
