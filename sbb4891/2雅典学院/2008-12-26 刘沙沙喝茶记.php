																											<div id="postmessage_14284" class="t_msgfont">（四）<br />
<br />
――刚开始我想着没多大事，想着宽松交流，所以根本不害怕。中统特务拿“判刑几年”恫吓时我一下被激怒了，也根本不怕。和他们几个对吵时情绪亢奋激烈，忘了害怕，然而，现在一个人被丢在这里，旁边是几个流里流气的保安在说笑打闹，我一个人默默地睡着，半夜三点，夜色里，是真的害怕了。<br />
<br />
我是一个人来的。亲戚朋友一个人都没说。警察也知道我一个人都没告诉，他们问我时，我如实回答了――网友们知道我来了，可下午送完东西我已经在网上发布了 “平安无事”，现在警察把我抓来没一个人知道，说句难听话，麻袋一装，扔进长江，警方一点风险都没有：她在南京走丢了，谁管得着？谁有证据？守望之鹰的话：“让你死都不知道怎么死的。”李寒秋的话：“直接毁尸灭迹。”皮诺切特时代失踪的上千人，尸骨何存？我这样的小蚂蚁，只是历史的沟壑里微不足道的一具尸骨，千万中之一，默默无闻，沧海一沙，民主的分母而已。<br />
<br />
杀人如草不闻声。<br />
<br />
不过，最可怕的还不是这个，真到了扔长江那一步我也就横下心痛快了：“杀了我恰恰证明我是对的，证明你们是不折不扣的法西斯！”民主的分母，而已。我不担心一下被杀那几秒钟，我担心的是长长远远时时刻刻的折磨，就象中统特务说的“判刑几年”。我一个外乡人，没根没秧没亲没戚的丢在南京监狱里，被犯人打是肯定的，被警方授意犯人们殴打是肯定的。这几年，再让老父老母为我操心，我爸我妈身体都不好，行动不便举止蹒跚，关上几年，不知出来还能不能见着他们――想到这些，揪心的痛，只能狠狠心不想。<br />
<br />
然而，所有这些痛苦和恐惧，都不能让我让步。或者说，都不能让我让步到我的底线之下。<br />
<br />
我不是新民党员，以后也不想加入。这一次来，是出于同情，“我不赞成你说的话，但我支持你说话的权利。”出于信用：“我说了要来，就要守信用”。来送完东西就回家了，游＊行的条件不成熟，游＊行计划放弃。这都是我本来的想法，明天跟他们解释清楚就是。<br />
<br />
刘沙的让步，仅止于此。<br />
<br />
至于其它：中国的多党制建设，民主追求，以及――朋友。这些，一步也不能让！一寸也不能让！让一寸，则刘沙今生今世休谈民主，今生今世，再无资格谈论民主！那样的羞辱和痛苦，那没有意义的人生，更痛苦。<br />
<br />
民主，朋友。如果连这点底线都不能坚守，则刘沙的骨气，是不是也太可怜了些？中国民主，是不是也太可怜了些？<br />
<br />
――我不敢保证，我能拿出第一秒的，民主的勇敢；但我敢保证，我能守住，最后一秒的，民主的羞耻。<br />
<br />
…………<br />
<br />
主意拿定后，心态也放松了。老保安开了暖风空调，我稍微好受一些，就是几个年轻保安一直在说话，最后实在累了没人说话了就在我旁边打电子宠物，不停的出怪声，吵我。睡几秒，又被闹醒，睡几秒，又被闹醒。一夜，这睡着的几秒几秒，积攒不到一个小时――困倦钝痛的脑袋，岌岌可危的圆凳，噪声，辗转反侧，天色微明。<br />
<br />
（五）<br />
２１号主审的，是头一天的尖下巴，和另一个中年警察。这个中年警察，四十多岁，长方脸儿，白，平顺的眉毛，双眼皮大眼睛，斯文英俊，很象年轻时代的汪精卫。穿一件黑色外套，举止大方、谈吐得体、引经据典、头头是道。我给他的编号是“教授”。当然，他现场发挥的辩证法认识论之程度，在我掐过的网友中只能算中上，当教授，肯定欠精深。但是，现在的教授，混事儿的也多――<br />
<br />
晨光中，尖下巴和教授两个人，来把我叫醒：“小刘，起来吃饭。洗漱没？带牙刷牙膏没？”我：“没带，就指着用宾馆的……”尖下巴马上对教授说：“去给她买一份。”教授转身往外走，被我叫住了：“给你钱。”“不用。”“你花钱不还是纳税人的钱？”（我不愿意从现在开始就占纳税人的便宜），他一怔：“我回来肯定找你报。”出去了。<br />
<br />
到得楼上，昨天那个办公室。不大会儿教授买回来牙刷牙膏毛巾一套，我给了他钱。去洗漱，回来吃饭：三个人一样的早点：塑料袋里的三个饭团，夹油条夹咸菜的饭团。豆浆。尖下巴给我倒了杯豆浆，我“谢谢不喝，我喝咖啡。”自己包里的咖啡冲上一袋，准备提神。<br />
<br />
饭团很干很油我吃不下，几口就放下了。等着他们吃完，三个人在桌前坐定：我和尖下巴的小老头警察，隔着办公桌对坐，他后边另一张办公桌，是教授。<br />
<br />
今天，“个人基本情况”，我让尖下巴在电脑上直接复制粘贴。几分种弄完，我开讲：“今天，先说三件事。”<br />
<br />
“一，昨天你们有人说，我想把李晶也拉进来，拉进这个行动中来。这个，冤枉。我没这个意思：我一直认为，夫妻两个，不能两口子都搞政治。那样的话，家怎么办，孩子怎么办？郭泉已经进去了，如果李晶再搞政治，那么孩子太可怜了。我只是对她致以同情，没有把她拉进来的意思。”<br />
<br />
“二，昨天你们有人说，我想跟着新民党捞个一官半职，这！――这是个很可笑的说法！我天生不适合当官，原来当过两年团支书，太累，组织生活，太琐碎，太累，不文学不浪漫！我没有当官的想法。这次来，一是为了同情郭泉，‘我不同意你说的话，但是我支持你说话的权利。’我不赞成新民党，但是我赞成他建党的权利。二是为了守信用，已经说了要来慰问，必须完成我的信用。来了，慰问了，就回去了。没有把李晶拉进来的想法，也更没有什么什么跟着郭泉捞个一官半职的想法，郭泉的新民党，就他一个人，离成功，那还远得很呐，渺茫得很呐，说‘捞个一官半职’这个说法（我被怄得哭笑不得），这是个很――很不沾边，很庸俗的说法！”我笑趴了，心里说，这是个很土匪、很共匪的说法。但看看面前这俩不象共匪，不想一杆子打翻一船人，没说出口“匪”字。<br />
<br />
“三，你们问我本人的情况、想法、行动、今后打算什么的，都行。但是，问别人，一字不提！”<br />
<br />
“问别人，一字不提！”这七个字，我一字一字咬得特清晰，特坚决，斩钉截铁。<br />
<br />
他俩都怔了一下，没想到我前边说得那么软，硬头钉子钉在这儿了！<br />
<br />
教授：“你不说我们也掌握了。”<br />
<br />
“对，ＱＱ记录你有，电话你们有窃听。所以我的‘一字不提’，不是证据问题，也不是态度问题，而是气！节！问题！如果我连这一点气节都没有，你们会瞧不起我的。”<br />
<br />
“还有，你们不要跟我说‘别人把你出卖了’这话，我特讨厌这话。同一件事，你们一会儿跟我说‘坦诚’，一会儿又说别人把我‘出卖’了，那我要跟你们坦诚了，一回头你们不是要跟别人说我，刘沙，把人家出卖了？我决不接受‘出卖’这种耻辱落到我身上。所以就是，问我的事儿，可以，咱们这是坦诚交流。问别人，一字不提！”<br />
<br />
响亮干脆的说完，他俩的表情都停滞了。<br />
<br />
…………<br />
<br />
停滞了一小会儿，尖下巴：“好，咱们开始。你为什么来南京？”<br />
刘沙：“来看郭泉。”<br />
……<br />
……接下去，就是整整一个上午的厮杀混战，三个人，三个头脑如大风中的柳絮球，飞快地从一个话题滚到另一个话题，从“郭泉的有罪无罪”到“Ｕ行Ｓ威对不对 ”，从“真理的可知不可知”到“今天的早饭不好吃”，从我的“普世原则、民主前景”到他们的“中国国情、戒急用忍”，高手过招行云流水，每次停顿，都是卡在了“别人”。<br />
<br />
争论得热火朝天时教授突然问：“游＊行示＊威谁提的？”我说不知道，当时群里气氛太热烈了，大家七嘴八舌都在说，不知道谁提的。他说我提醒你一个词，是不是有人先说了“散步”？我一看他又想去翻ＱＱ记录，一下急了：“我提的！我先提的，我计划的！”<br />
<br />
脑袋高高的昂了起来，坚决地盯住他，特务威胁的几年徒刑从天而降压在我肩上，却压不垮我的豪情和担当：“我提的！我计划的！如果你们因为策划游＊行判我几年的话，那是我的骄傲和自豪！”<br />
<br />
他眼神停滞几秒，刚想说话我又给打断了：“别跟我说什么‘别人出卖你’，我也不接受囚徒困境。你就是把别人出卖我的笔录给我看了，别人出卖我的录音、录像当面给我放一遍！我也不会出卖别人！所有人都出卖我，我也不会出卖别人！宁可天下人负我，不可我负天下人！我就是这么一个人，就是这么样的人，这是气节！”<br />
<br />
空气和阳光一下静止，静如黄金的页子，敲之有声。<br />
<br />
金声玉应。<br />
<br />
……<br />
<br />
停了几秒后我怔怔地问教授：“我是不是很傻？”――<br />
<br />
一个自豪的气球儿悬在空中，等着对方的承认和夸奖。<br />
<br />
教授阴郁地剜我一眼，阴狠的挖苦：“我觉得你很聪明！――你一直在避免回答一个关键的问题！”<br />
<br />
“哦。”<br />
<br />
“啪”一声气球被戳破，我没拿到夸奖，“吧叽”一下掉了下来，沮丧了两秒种，不过――“你很聪明”，也算夸奖，于是又得意起来。<br />
<br />
尖下巴又问话了：“你什么时候来的南京？”<br />
<br />
“昨天早上。”<br />
<br />
“到南京后和谁联系了？”<br />
<br />
“我不回答你。我说过了，事关别人，一字不提！”<br />
……<br />
……<br />
……<br />
平心而论，二十一号上午这场辩论，从十点以后，就是刘沙赢得少，输得多。几乎对方每一轮理论攻击，都是在我这里告终。要么就是我的“保留，咱们保留意见，谁也说不服谁了，瞧两边都开始循环论证了……”要么就是我怔怔地听对方说着，却接不上话来。然而这样的输，刘沙不服：毕竟我头天只睡了不到一个小时，头脑钝痛黑暗。对方的理论，都是论坛上常见的挺政府言论，早就是被我掐得死去活来的手下败将。可，在警察局一夜失眠的情况下，在这种二对一，三对一的不平等辩论条件下，常常是，一个人被我说住了，逼到死角里了，另一个马上打岔，逼迫我那疼痛破碎的脑子跟着转换思路，这种转换，很痛苦。再不然就是，对方说的我不服，我心里有话辩驳，可，头脑黑暗疼痛，眼前黑暗混沌――这样的赢，他们胜之不武，这样的输，刘沙虽败犹荣。<br />
<br />
我体力不支，打着盹儿不服：“你们不让我睡觉，搞疲劳战，你们胜之不武！――”<br />
<br />
不料尖下巴和教授齐齐叫苦：“我们哪睡了？你睡了我们还要整理文件，我们都没睡！”尖下巴：“你看看我们的黑眼圈！”<br />
<br />
果然俩人都是黑青眼圈。怪不得教授的表情那么阴险呢，都是黑眼圈给闹的！我是又打盹，又想笑，正盹着呢，郭粉青着眼圈进来了：“怎么样今天？”“问着呢。”<br />
<br />
郭粉坐在我旁边，又跟我叨叨叨叨，我强睁着睡眼听着，叨叨中只听清了他一句：“你傻！――人家十个人给你挖的坑，就等着你跳下来呢。”我说：“我知道。”郭粉：“你知道你还护着他们？”<br />
<br />
教授懊恼地挖苦着：“人家那是气节！――”凶狠地嗔我一眼，嘴角却笑。真象一个班主任，带了一群又淘又倔的闯祸学生，审“逃学打架”审不出口供，恼火得，又好气又好笑：“人家那是气节！――”<br />
<br />
郭粉就怔着看他：“哦，气节？”<br />
<br />
满屋子人都笑了。我一边笑，一边接着打盹。<br />
<br />
……<br />
<br />
“你傻啊，多少人给你挖好了坑，就等着你跳下来呢！”<br />
<br />
我知道。<br />
<br />
我想让他看看我的散文：《艺人的琴弦上永远传唱着你们的故事》，让他看看那深刻和沉痛：<br />
<br />
――我对革命的纯洁度早已不抱任何幻想，每一场革命里都有它的心机与龌龊。你的领导人时刻准备着出卖、出逃，你流血牺牲只保留了如此自私的“火种”！<br />
<br />
――明知道，在我所参加的任何一场运动中，我都会是首先被出卖的那一个。然而，即使知道后边有人在出卖我，我也仍然会――往前冲。<br />
<br />
不悔。<br />
<br />
总得有人傻，总得有人牺牲，总得有人出逃，总得有人活下来，踩着着同志们的累累尸骨登上城楼：“人民万岁！”――<br />
<br />
代代革命，代代如此。共产 党、国民党、民进党，没有谁清白，个个都是罪人。<br />
<br />
政治之肮脏，从来如此，而当今中国，不能只让共、产、党一家龌龊！不能只让共、产、党一家，没边没沿地龌龊下去！必须要有另外的政党，另外的心机手段和共、产、党抗衡。龌龊对龌龊！<br />
<br />
如此，才能形成有力的竞争。才能逼迫各家党派都知道点儿要脸，知道点收买民心。<br />
<br />
革命是――收买、煽动、挑拨、激将、青春热血、老谋深算、纯真理想、狼心狗肺、权术、炮灰、驱使同志们卖命而自己身登高位――如此卑鄙，如此肮脏，历史，就这样肮脏着前进。<br />
<br />
不悔。<br />
<br />
