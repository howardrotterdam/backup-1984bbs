																											<div id="postmessage_14283" class="t_msgfont">（三）<br />
<br />
……<br />
<br />
在我睡眼朦胧之间，中统特务要从我面前过，要我的二郎腿让一下。我弯弯腿，让他过去后小声骂：“你个特务，你个中统特务――”他已经走到另一张桌边坐下了，听我这么骂他，又给气笑了――<br />
<br />
他又走回来，坐到我这张桌边，一张口，就被我堵了回去：“我讨厌你，我不和你说话，我不回答你的问题！！！”<br />
<br />
然后就是困倦得，低着头闭着眼，他又问了好几个问题，都被我的疲倦挡在了脑海之外。结果他生气了：<br />
<br />
“你这是什么态度？”<br />
<br />
我头都没抬：“江姐的态度。”<br />
<br />
他被怄得哭笑不得：“那你是江姐，我们是什么？”<br />
<br />
我心说“共产 党反动派”。&nbsp;&nbsp;~_~<br />
<br />
但是，“反动派”三字一出，就又得是一通长篇大论的争论，什么是“反动”，“ＧＣＤ怎么从革命到落后”以及，烈士们为什么要喊“打倒国民党反动派”而不是“打倒国民党”等等。我困倦得不行，没精力和他长篇大论的争，所以“共产 党反动派”也没说出口，只闭着眼养神。<br />
<br />
他又叨叨叨叨说了Ｎ多，看我没反应，发狠了：“你别以为我们就拿你没办法！就你网上的反动言论，早就够判好几年的了！”<br />
<br />
我激怒之下，一下昂起了头！高高的昂起了头，不看特务，而是盯着墙壁，嘴角紧绷，眼神倔强，那架势――眼望五洲、心怀四海、大义凛然、宁死不屈、杀剐由你、“杀了我恰恰证明我是对的，证明你们不折不扣是法西斯！”我当时真是激怒了！真是横下心豁出去了！骄傲极了！轻蔑极了！<br />
<br />
眼角余光知道他在盯我，但我决不看他！两个人就这么僵持着。僵持了Ｎ久，看他真说不出什么话了，没新意了，我就闭上眼，又睡觉了。<br />
<br />
一旦刘沙闭上嘴不说话了，谈话就很干巴很无趣了。就他们六个人轮换着叨叨叨叨，那长篇大论的陈腐叨叨我记不住。而且我也太困，只记住了几个点儿：<br />
<br />
（跟我说话的一个白净方脸警察，我管他叫“郭粉”，因为他是郭泉粉丝　~_~　　）<br />
<br />
（１）郭粉：“你别以为这次只抓了你一个，这次我们抓了好几个！各地都在动手抓人！宁文忠已经抓起来了，邬伟民也已经抓起来了！”<br />
<br />
我眼睛都没睁，也没什么反应：不意外。<br />
<br />
（２）郭粉：“你给李晶送的东西，放在传达室，人家也没去拿，人家也瞧不上这点礼物。”<br />
<br />
我心说：挑拨。<br />
<br />
（３）郭粉：“你就那么困？那么没精神？那么没精神你还搞什么民主？你孬种！听明白没有？你孬种！”<br />
<br />
我心说，你们孬种，六对一，你们才孬种！<br />
<br />
（４）郭粉：“郭泉就不孬种，人家比你精神多了！趴到电脑上可以几天几夜不睡觉的！跟我们辩起来可以一夜不睡的！”<br />
<br />
睡眼朦胧间，看到他提起郭泉时，那感叹钦佩的眼神。那眼神亮亮的，我心里也一亮，啊，郭泉有警察粉丝啦！<br />
<br />
（５）郭粉：“民主建设不可能一步到位，郭泉就有最精彩的一句话是：‘象我这样的，在五十年代，早枪毙了，六七十年代，肯定无期徒刑！而现在，我能在这里跟你们辩，就是中国民主的进步！’”<br />
<br />
我心想：不够，远远不够。<br />
<br />
又一想，啊啊，郭粉，被自己的偶像承认了“进步”，瞧你高兴得！<br />
<br />
（６）郭粉：“你们搞民主的人，应该讲诚！信！，讲坦诚！郭泉就很坦诚，有什么说什么。整天就是‘你们为什么还不来抓我啊？为什么还不判我啊？’”<br />
<br />
我心想：一，坦诚？我跟谁坦诚也不能跟你们坦诚，我可以拿自己的事坦诚，但决不能拿朋友的事坦诚！<br />
<br />
二，逅溃郭泉郭泉郭泉，你没发觉你提到郭泉的次数太多了吗？而且，郭泉这话，也真是，你们是互为斯德哥尔摩囚徒了吧？<br />
<br />
（７）郭粉：“我和郭泉吃过不下十次饭！其实郭泉人不错，不坏，就是他不知哪根筋拧上了――”<br />
<br />
我心想：着急啦？心疼啦？　　~_~<br />
<br />
（８）郭粉：“来看郭泉的人多了，我们抓了好几个了，丽水的邬伟民，合肥的某某某，都抓起来了，也都回去了――人家早就把你说出来了，早就把你出卖了！”<br />
<br />
我心想：一，挑拨。<br />
<br />
二，切，你前边跟我说坦诚，后边又说别人“出卖”，那我要跟你们“坦诚”了，一转身你们肯定跟别人也要说“刘沙早就把你给出卖了！”让我的朋友们寒心？<br />
<br />
（９）郭粉：“悖”鹚了！去水房洗把脸，清醒一下，别睡了！（踢我椅子）你是不是睡得太舒服了？来换把椅子！”在我的皮面扶手椅子旁边，扔下一张只有靠背的硬椅子。<br />
<br />
我睁眼看看：“然后就该站着了，然后就该蹲着了？”<br />
<br />
他们几个哈哈大笑：“你把我们看得也太可怕了吧？你都从哪儿听说的？”<br />
我闭上眼睛低着头接着打盹，他们把我架到那张硬椅子上。<br />
我还是打盹。<br />
<br />
（１０）郭粉：“你有什么病没有？如果有，可以提出，我们好安排照顾。”<br />
<br />
我很想告诉他们我头痛了两三年，心脏也不太好，以前莫明其妙晕倒过，这会儿也觉得心跳胸闷―――但，咬咬牙没说：一则不想让他们发现我有弱点，二则，不想向他们示弱求援。不想承他们的情。<br />
<br />
（１１）郭粉：“你什么‘看李晶’，你们不就是想把李晶也拉进来，拉进你们的行动里来吗？”<br />
<br />
（１２）此时一个穿黑色警服的警察进来了（别人穿的都是便衣），此人是个流氓土匪，纯的。我给他的编号是“土匪”，土匪言论大致如下：<br />
<br />
土匪：“你是干什么工作的？”<br />
刘沙：“油田。”<br />
土匪：“你怎么去的油田？”<br />
刘沙：“考去的。”<br />
土匪：“为什么你就能考去？”<br />
刘沙：“企业需要劳动力。”<br />
土匪：“企业是谁办的？还不是共产 党办的？你的饭碗，还不是共产 党给的？”<br />
我一听他是如此奴性的感恩逻辑，知道双方水平相差太远，再也不理他了。此后就是他在说，我在轻蔑，他每说一句，我就冷笑一声，冷笑他那奴才逻辑，那种把正当的劳动报酬当中共恩德的奴才逻辑：<br />
<br />
“你为什么能去油田？别人为什么就不能去？还不是共产 党给了你饭碗？还不是共产党给了你爹妈饭碗？你的饭碗是国家给的，你父母的饭碗也是国家给的，你从小到大吃着国家的，喝着国家的，现在颠覆国家？想推翻共产 党？想推翻共产党？想跟着郭泉干，推翻了共产 党捞个一官半职？”<br />
<br />
他说一句，我冷笑一声，最后一句话，我一下笑趴了！他看看如此被轻蔑，也觉得无趣，悻悻然几秒，悻悻而去。<br />
<br />
然后，就是我打盹，郭粉、特务、尖下巴三个人的激将、威胁、劝慰、红脸、白脸、花脸――　　一锅乱七八糟的语言粥，翻滚的语言粥，离我很近，离我很远，与我无关。<br />
<br />
我只管闭着眼，紧绷着唇，打盹，那表情，就是个抵触！<br />
<br />
最后他们终于说累了，（可能对望了一下），放弃了：“来，笔录签个字，签个字去睡觉。”<br />
<br />
睁眼看了一下，笔录上结束时间是一点半，内容大致不差，我一盹一盹的，在朦胧睡眼中签了字，字写得东倒西歪能有鸡蛋大――<br />
<br />
然后一个年老保安，引着我出办公室，我在走廊里半睁着眼睛往前走，走不两步就走到墙上了，再走没两步又走到墙上了。就这么黑一下，明一下，在墙上扶一下、扶一下走到走廊尽头，身后的郭粉挖苦我：“瞧你这精神头儿，你比郭泉可差远啦！”我心里顶嘴：沙皇的战斗力可不在这会儿，不在熬夜掐架上边！――<br />
<br />
老保安下了楼梯，我站在楼梯口，停一下，晃了一下，郭粉马上在后边叫：“接着她，小心她摔下去！”老保安马上站住，仰脸看着我。我苦笑笑，扶着栏杆，一步一步蹭了下去。<br />
<br />
郭粉在后边嘲笑：“就你这困劲儿还搞什么民主！这才是第一天！”我心想，把我搞成这个样子，是你们的耻辱，而不是我的耻辱。<br />
<br />
睡眼朦胧中拐了几个弯儿，越拐楼道越灰暗越压抑。最后他们在尽头一个空间停住：“你今晚就睡这儿。”<br />
<br />
我过去一看，沮丧了：靠墙三个小圆凳，就是银行柜台或酒吧吧台里那种小圆凳儿，坚硬光滑的小圆凳儿，直径三十厘米左右，还有一小半圈儿是斜斜的靠背。就是说，只有二十厘米左右是平的――<br />
<br />
尽管很受打击，但我也决不想再向他们求助求援，不想说好话。而且也没精神再争。就爬到那些圆凳儿上，和衣睡下了。<br />
<br />
事后想想，这是他们一个卑鄙的地方，他们决不会让你睡足了有了精神，头脑清晰、思路敏捷的和他们辩论。这也是一个教训：该要的人道待遇一定得要，吃饭睡觉治病什么的，一定得要！不要想着“脸皮薄不愿意求人”。否则你没法保持体力第二天和他们斗争或者说交流――民主主义者和人权主义者首先要争取的是自己的人权，连自己的人权你都不能去斗争保卫你谈何保卫别人？<br />
<br />
……朦朦胧胧睡了几秒，又朦朦胧胧被吵醒――被旁边走来走去、说笑打闹的保安们吵醒。我把脸埋进胳膊弯里，把自己埋进黑暗的夜色大海深处。开始思量。<br />
<br />
开始害怕。</div>

							
							
							
							
