																											<div id="postmessage_14285" class="t_msgfont">（六）<br />
<br />
中午是吃的盒饭。吃完后回办公室，我趴到桌上，接着打盹。教授和尖下巴，悉悉索索在整理文件。<br />
<br />
打着打着盹我的激情又上来了，一抬头，眼前金光灿烂：“你们知道我这会儿什么感觉吗？”<br />
<br />
他们都不解地看我。<br />
<br />
“我这会儿的感觉是――幸福！一则因为你们水平都高，跟你们掐架，智力上很爽。二则是，终于轮到我站在时代的前沿了，办到我来推动社会的发展了！这感觉，特充实，特幸福！”<br />
<br />
教授又恼火，又想笑：“你呀，太幼稚！”<br />
看我是一脸的不服，他斟酌着词句，看着我的脸色，斟酌着词句：“你是――你别怪我说话难听啊？――你啊，你是没孩子，等你有了孩子，你就知道你的想法有多幼稚了！”<br />
<br />
“哦。”<br />
<br />
我实在想不出来育儿经和推动社会有什么必然的、逻辑的联系，不服。但问题是我确实没孩子，这点上没法跟他争嘴，只好怔着。<br />
<br />
他又坐回桌后边：“我们是尽力帮助你、挽救你，你呢，对我们是敌对的态度。”<br />
<br />
我：“这就象个童话情景：大狗和小狗儿咬架，大狗根本没把小狗儿当回事儿，可小狗儿看来，哇欧！　@_@　好！可！怕！”<br />
<br />
他又笑了：“我们可没想让你觉得可怕！怎么样，这次亲身体验了吧？我们没把你怎么样吧？没那么可怕吧？”<br />
<br />
我一边点头，一边却想起昨天的中统特务，那真是一条凶恶的大狗！　-_-|||<br />
<br />
……在我接着打盹的功夫，他们交接，换人了。这次换的是一个四十多岁的女警，和一个小帅哥。这个小帅哥，昨天去抓我的时候就有他，帮我把零钱硬币收起来的，就是他。他一张稍稍发胖的鹅蛋脸，白皮肤，平眉，单眼皮大眼睛，眼睛下边一道精美的卧蚕，普通漂亮的鼻子和嘴巴――总之，你可以想象一下单眼皮的钟汉良，或胖了一点的苗侨伟――此人看上去最帅，水平却最菜，我给他的编号是：“脑残”。<br />
<br />
他在和女警聊天儿，我趴了一会儿，起来走动时，看见他那模样，不禁想笑：眼圈黑青，尤其下眼皮，好象用毛笔生生画上去一道黑。我就看乐了：“你都干什么去了也熬夜了？眼圈也黑成这样？”心想昨天我进来后他就消失了，没审我，那他审谁去了？他们到底抓了几个人？<br />
<br />
他也乐了：“哟，这么关心我？”<br />
<br />
我笑着答：“不是关心，是好奇！――”<br />
<br />
不是关心你，是关心我的同志。<br />
<br />
这个办公室，房间到阳台的墙给打掉了，原来窗台的地方放了一张办公桌。现在太阳已经到了西边阳台上，房间明亮通透，我就坐在阳台上，办公桌后边，晒暖儿，打盹。<br />
<br />
盹了一会儿，脑残――算了！　　~_~　　尽管这孩子蠢了点，可我毕竟嗑了人家一包瓜子，不好意思老是“脑残脑残”地叫，改个称呼：钟汉良，小哇。<br />
<br />
小哇问我：“你一个女的，怎么会对政治这么感兴趣？受谁影响？”<br />
<br />
我说：“我是受林达的影响。”<br />
<br />
“林达？……”他的眼睛和胖脸一下僵滞了。你跟一个人谈论一个他根本不知道的、他反应不过来的问题，看他脑子转不过弯来时，那愚蠢迟钝的表情，那就是这样的表情。不过，公平说句话，这种表情，这两天，我肯定也没少出现，想必警方也看在眼里了。惭愧，惭愧。<br />
<br />
他迟疑了一下，眼珠笨拙地转了一下：“林达？……是个什么人？”自以为是的警觉眼神，发现了新罪犯的眼神。<br />
<br />
“是个旅美华人，写了不少文章，国内到处都有他的文章。”我心里惊讶叹息，你连林达都不知道，你还怎么办政治案啊弟弟？！<br />
<br />
他固执地追问：“你跟他有联系？”<br />
<br />
我哭笑不得：“他的书都是公开出版的，国内到处都是，我没跟他联系过！”<br />
<br />
然后我就跟他讲了《总统是靠不住的》，“国会是靠不住的，独立检察官是靠不住的，最高法院是靠不住的，只有这种互相制衡互相约束的制度才是靠得住的――”只听他嗯嗯连声，也不知听懂了没有，我又太累，说完这几句，桌上一趴，接着睡。<br />
<br />
他出去了，一会儿进来，让那个女警：“吃瓜子儿。”“不吃。”他又叫我：“哎，吃瓜子儿！”我：“谢谢不吃。”继续睡。<br />
<br />
――他就自己嗑了起来，不大会嗑得满屋那个香啊！那个瓜子香啊！我经不住诱惑，睁开眼起身，把他那包瓜子倒出来一把，也嗑。<br />
<br />
正嗑着呢，女警进来了：“哎，吃完这一把别吃了，马上上班了，人多了，看见了――”<br />
<br />
然后就是女警和小哇他俩聊天唠家常。我在阳台上晒太阳，闲得无聊，就把他们办公桌下边的杂志拉出来看，凤凰月刊什么的。<br />
<br />
看了两本杂志，又睡。女警过来了，还是昨天的工作、地址、工资什么的，又象问讯，又象拉家常，我不好意思不礼貌，只好抬起头回答。然后就是住房啦，父母啦，天气啦，喜欢看什么书啦――“喜欢看政史类的书籍，次贷危机，货币战争什么的，不喜欢柴米油盐、安玻璃啦、油烟机啦什么的话题。”女警感叹道：“哎呀那我感兴趣的话题你肯定不感兴趣。”然后又是天气“这边的法国梧桐还没掉叶子呢。”然后我：“院里菊花真好看，刚才我站在窗户边看菊花的时候就想着得多看两眼，这可能是我这几年最后一次看见这么好看的花儿了。”女警一怔：“怎么？”我：“昨天那个人不是说要判好几年吗？”心想，何况今天上午我还拼命的死顶，一口咬定是我干的。<br />
<br />
顺口说到这儿，说完了我又有点后悔，不想让他们看出我在寻思“几年”这事，显得我害怕了似的。<br />
<br />
小哇：“你油田生活不错为什么还干这些？”<br />
我：“生活是不错，可是，人生不能就这么过去了呀？总得干点有意义的事情吧？所以我就想到搞民主。得，这下得到监狱里去体验人生了。”我把重音放在“体验人生”四个字上。<br />
<br />
女警很意外：“到监狱里体验人生？”<br />
<br />
“是啊。”我摊出一只手，一五一十地跟她解释：“监狱里可以观察人性，特复杂、特深刻的人性。这也是人生体验啊？”<br />
<br />
出来我就可以写书了，中国版《库拉格》――想跟他们这么解释，但考虑到他们未必听过《库拉格》，也就做罢。<br />
<br />
反正我不怕你们。<br />
<br />
然后小哇接了个电话，又把我叫到他面前，隔桌坐下，问我为什么有两个手机号，来的时候在哪儿坐的火车：“谁给你买的票？”“自己买的。”<br />
<br />
女警突然问：“票价多少？”<br />
<br />
我：“忘了，不贵，所以忘了。”<br />
<br />
女警不忿：“自己买的还忘了？”我心说你是俗气女人你记得住，我是浪漫女人，我记不住。何况这两天这么困，更忘了。<br />
<br />
小哇：“你给李晶送完东西还打算干什么？”<br />
我：“玩儿，在南京玩儿。雨花台啦中山陵啦纪念馆啦什么的。”<br />
他突然袭击：“你不是第一次来南京吧？”黑灰灰的熊猫眼眯了起来，敌意的眼神，扮凶的眼神。<br />
我很意外：“我真的是第一次来南京啊？”<br />
“你对南京挺熟的嘛？”<br />
我笑趴了：“我有地图哎！何况南京的风土人情，几个景点儿，稍微看点书的人都知道哎！”<br />
<br />
心里感叹，这小Ｐ孩儿，真是蠢得没边儿。<br />
<br />
果然，他接着犯蠢，自以为是地想和我讲政治：“你认为共产 党好吗？”<br />
<br />
我再次笑趴了：“你这个问题很幼稚！――”<br />
<br />
然后，正色，诚恳地：“共产 党在历史上起过非常进步的作用。从一八四八年共产 党宣言发表开始，到苏联革命，中国革命，共产党在追求社会主义，追求平等的方面，有过不错的历史成绩。但是，从六七十年代开始，西方国家，在民主的基础上，吸收了社会主义的不少优点，这个时候，东方社会主义国家，民主性欠缺，就落在历史的后边了。”<br />
<br />
他笑了：“你的意思是共产 党有好的时期，也有不好的时期？”<br />
我也笑：“对，有好的方面，也有不好的方面。有好的可能，也有不好的可能。”<br />
他笑喷了：“总之你就是不肯正面回答问题。”<br />
我笑着反驳：“那是因为你问题本身太幼稚！”<br />
他换了一个自认为不幼稚的问题：“那你认为共产 党坏在哪里？”<br />
我心里想着最近那几件大案，口中却换了个不那么刺激对方的说法：“这不是个案问题，这是通则问题。人有缺点，党肯定也有缺点，所以需要多几个党，来互相监督。”<br />
小哇：“我们有多党，好几个党呢。”<br />
我笑：“那几个花瓶！”<br />
“你怎么能说他们是花瓶？”<br />
“当然啦，我听说这些党发展党员都限制，不能超过多少人，必须死一个，才能补一个。”（其实我想的不光是这个原因“花瓶”。但是，另一个思路并不成熟，也不严谨，容我另文讨论。）<br />
“我怎么没听说这种限制？南京也有国民党你知道吗？”<br />
这下轮到我的眼神停滞了：“……不知道。”几秒钟后又兴奋起来：“真的？……”开始浮想联翩。<br />
<br />
……<br />
下午的对话基本上就这样了。他打完笔录，要我签字时为了最后一句：“没有受到其它干扰”――为了这句“干扰”我们吵了起来：“刑讯逼供是没有，但是你们有诱供现象！――”小哇：“这是（文件）系统自动生成的――”吵了几句，最后是尖下巴和了个稀泥：“算了算了，拉掉――”才签了字。<br />
<br />
到楼下吃完盒饭，我又在那三个圆凳上盹了会儿。小哇叫我：“包背上，去旅馆。你们公安局来把你接回去处理。”<br />
<br />
夜色中一边往旅馆走，他一边跟我解释：“条件不好，也没办法洗（澡）了，你就合衣睡一会儿吧。”<br />
<br />
到地方一看，果然是一家又旧又脏的小旅馆，最里边的房间，门口已经有两个警察，进去一看，两张床，里边床上睡了一个不知犯了什么事儿的小伙子。他们让他起来，把外边床上没盖过的被子扔到里边床上，然后对我：“可以了，你睡吧。”<br />
<br />
我把羽绒服一脱，穿着毛衣裹进了被子里，真困了，在他们的聊天儿和电视声中，睡着了。<br />
<br />
醒来时已是深夜一点，斜靠在床头，看着电视，听他们说话，两点左右，听见楼道里好几个人打招呼寒喧的声音传过来，有人进来跟我说：“你们公安局的来了，起来吧。”<br />
<br />
进来的两个人，一个黑衣胖男子我不认识――后来才知道他是厂保卫科的――当时我眼里看见的，只有一个熟人：我们厂书记。进来看看我，一幅大哥哥来接淘气小妹：“看见你没吃亏我就放了心”的表情，跟我说：“……小叶来了，（小叶是我玩得要好的小姐妹），小叶在车里等你……”<br />
<br />
…………<br />
<br />
我起身的时候，“中统特务”站在房间门口等我出来，可能是累了，也可能是没任务，总之，他不再咄咄逼人，而是低着眼帘，默默的，侧身站在门口等我出来，有点不敢和我对视的样子。就象一个来接首长的卫兵，或者，刚被老师训了一顿的孩子。我突然有点后悔，觉得自己在道德上伤人太重了。他也是为了自己心目中的“ 工作”，却被我骂成那样，蔑视成那样……<br />
<br />
教授正面迎来，我一边和他热情话别，一边往前走：“你的水平在我掐过的对手中间，算中上。可是，考虑到你们是警察，已经很不容易了，总之，我对警察队伍的素质提高，还是满惊喜的。”他在我身后寒碜我：“你见的警察都是哪儿的啊？”我想说我见到的警察都是门难进，脸难看，话难听，事难办的。可是，毕竟我有家乡自尊，不想在外人面前说家乡的坏话，只好不答而行。同时心里还惦记着要跟中统特务握手致意：“高手，服了――”可是，他昨天被我骂得太厉害，羞怯了，这会儿远远地拉在后边，夜色里在和我领导交接道别。而我在接人待物上也是羞怯磕巴，心是想，人却不好再强巴巴赶过去、找话缝说话――这时已来到单位来车旁边，夜色里灯光下，我的小姐妹叶小丽从车里出来，叫“沙沙！”我扑过去一把抱住，两个人搂抱着亲热着说得热闹无比，我心里却还惦记着没能和中统特务道别，没能有高手揖别那豪爽的一抱拳：“保重，龙虎风云，后会有期！”</div>

							
							
							
							
