																											<div id="postmessage_717117" class="t_msgfont">一开始很“嘴硬”<br />
　　穆罕默德很精明，照片中的他显出焦虑和探究的神情，这是半夜里突然被唤醒的人的面容。<br />
　　如果穆罕默德在最初几个小时感到绝望，他也没有表现出来。据进行首次讯问的巴基斯坦三军情报局官员称，这个“基地组织”的重要头目看上去很镇静，显得很有自制力。在刚被捕的头两天，他除了确认自己的名字，什么话也没说。一名CIA官员说穆罕默德那两天“处于恍惚之中，只是背诵《古兰经》经文”，到第三天，他开始懈怠了。他用一连串的乌尔都语、普什图语和俾路支语辱骂巴基斯坦审讯官，向他们宣扬穆斯林的责任，谴责他们与异教徒合作。“充当美国代理人不会有助于你们的国家，”他说，“有很多像我这样的人宁愿去死也不会让美国人得到片刻安宁。”当问道本．拉登是否依然活着时，穆罕默德说，“他当然活着。”他说去年12月份他曾在“边境的山区”与本．拉登见过面。对于美国将要发动的伊拉克战争，他显得有些自鸣得意。“让伊拉克战争开始吧，”他说，“美军将在海湾的基地里成为目标，我没有什么明确信息，但我的第六感告诉我，你们会在沙特、卡塔尔和科威特看到要发生什么。”的确，此后不久“基地组织”就在沙特发动了恐怖袭击。<br />
　　残酷的折磨<br />
　　就在那一天，穆罕默德又被蒙上头送到位于拉瓦尔品第的查卡拉空军基地，转交美军关押。从那里他又被转押到阿富汗巴格拉姆的CIA审讯中心，过了一些日子后，再被送到一个“秘密地点”（CIA称那里为“加利福尼亚旅馆”），估计是在另一个与美国有合作的国家，或者在一个特别设计在航空母舰上的监狱里。在哪里无关紧要，因为穆罕默德对这个地方不会熟悉，不可能知道是哪里，时间地点和推断的依据都会被隐藏起来。他可能会进入一个新的空间，在这个奇怪的空间中，他的每一句话、每个动作和感情都会受到监控和测量。在这里，眼见的东西可能与真正的东西不一样，可能没有白天和黑夜，或者没有正常的吃喝醒睡方式，在这里，冷热干湿脏净真假都会被扭曲颠倒。<br />
　　当他到达一个较为固定的场所以后，可能早就给折磨的疲惫不堪、饥寒交迫，即使不为自己的处境感到害怕，也会为妻儿们的状况而恐惧。他的家里人可能一同被抓起来了，也说不定早几个月前已经被捕。他会受到警告，如果不好好合作，就会由一些残酷无情的第三国审讯者来收拾他。他极有可能被扒光衣服锁进一个看不到一丝天色的小笼子里，日日夜夜受到强光和噪音的刺激。这个笼子小得他都站不起来，坐也坐不直，当然也躺不下去。他会浑身冰冷湿透，要想打盹，马上就会被唤醒。他想得到东西吃可不大容易，即使有也不会规律，而且食物量很少，一点滋味也没有。<br />
　　有时候他会受到连续几天的讯问，有时候可能讯问只有几小时甚至几分钟。人的大脑急需规律，几乎能够适应任何出现在面前的任何事物，因此监管人员会绞尽脑汁不会让任何规律性的东西出现。<br />
　　大麻用于审讯<br />
　　他所受到的审讯强度很大，有时候审讯人员声音非常大非常粗鲁，有时候又很轻柔很友好，态度的变化不会有什么明显原因。有时候会一遍又一遍地问他同一个问题，然后突然换成另一个完全出乎他意料之外的问题，它可能是某个秘密或细节，他会震惊地发现审讯人员原来已经掌握了他。审讯人员会给他获得自由或使妻儿们的状况好过一些的机会，如果他对审讯人员有帮助，或者他提出的情报是真的，他的待遇会得到改善，如果情报是假的，那么他会受到更粗暴的对待。有时候在提审他之前，审讯人员可能会用药物让他兴奋起来，大麻、海洛因和巴比妥盐对撬开犯人的嘴都很有用，而甲基苯丙胺之类的中枢兴奋药能让嘴最硬的家伙也变得滔滔不绝，只要话多，就不可能编造出复杂的谎言。这些药物会秘密地通过食物饮水给他服下去，由于他受到的待遇极其恶劣，药物甚至会给他带来片刻的慰藉和欢愉，于是他会对药物产生一种新的渴求，审讯人员又有东西可以利用了。<br />
　　由于任何外界信息都被刻意隔绝，穆罕默德就会越来越容易受到操纵。如“基地”组织袭击科威特和沙特之后，审讯人员可能会把收集到的情报告诉他，只是片言只语，却能暗示完整的行动。在审讯中，他经常会对透露给他的其组织的一些详细信息感到震惊，如正在进行的情报活动、新的逮捕或对其他已经被捕的“基地”组织成员的审问等细节。有些给他的情报是真的，有些是假的。审讯人员会对他说，他的一些关键的同伙现在很合作，或者完全背弃了圣战事业。日复一日，他知道的东西越来越少，而审讯人员则在进步。他可能会认为过去策划的一些重大行动实际上成为无关紧要的，或者早已被美国识破。某些秘密的重要性逐渐显得无所谓了。<br />
　　由于极其孤单、困惑、疲倦、恐惧，并一再受到折磨，穆罕默德渐渐对一些简单需要产生渴求，这些需要都由审讯人员所控制。<br />
　　而满足这些渴求的钥匙只有一个：说。<br />
　　迷幻药的功效<br />
　　在审讯艺术的发展过程中，人们一直在寻找能获得真实招供的方法，但总的来说没有什么结果。纳粹科学家在关押在集中营的人身上曾经做过实验，让他们呆在高温和极冷的环境中，给他们服用药物，并且运用刑讯，以图了解把什么恐怖结合起来会使人合作。这种实验弄死弄残了很多人，但没有找到让人开口的有效方法。<br />
　　审讯者对迷幻药（LSD）抱的希望更大，这种药是一个瑞士药物实验室偶然发现的，一小剂量就能产生强烈的意识改变效应。LSD比墨斯卡灵这种致幻剂更为有效，不过也有很多人喜欢后者，因为它能很容易的混入食物和饮水中给受审讯者服用。他们希望在这种人为的没有防范的状态下，受审讯的人会忘掉目标和忠诚，成为任审讯者捏的泥团。药物带来的恐惧和焦虑会转变成可怕的幻觉，因此更难得到被审讯者的秘密，还使获得的信息带有虚假成分。LSD可能会释放意识中深层次的感觉，但真正的秘密却很难挖出来。<br />
　　研究人员还曾实验过海洛因和迷幻蘑菇，这两种东西都不能有效地让人说出心底的秘密。的确，药物看来加强了一些人的骗人本领。车莨菪碱这种镇静剂也给人带来过希望，但它经常导致幻觉。巴比妥酸盐很有前途，心理医师已经有效的把它用于治疗。大麻、酒精和甲基苯丙胺能降低抑制力，但无法消解心底最坚定的信念。而且药物效力越大，招供就越不可靠。如今在一些关键的审讯中，会把药物当做辅助手段，常用的药物是用巴比妥酸盐和大麻调和的甲基苯丙胺。这种工具能带来帮助，但要看审讯者如何使用。<br />
　　感觉剥夺<br />
　　感觉剥夺和单独监禁看来更有效果。对大多数人来说，如果感觉被严格剥夺，会迅速变得非常痛苦。在声名狼藉的1963年CIA审讯手册《库巴克手册》里，记录了感觉剥夺的效果，它是迄今为止最深入详尽的解释强制审讯法的书。不过官方是不愿意讨论这些问题或把它们印成书的，因为这种事既会造成政治尴尬，又属于秘密。公开签署的审讯条约主要是适用于警察部门的，着眼的对象也都是刑事被告，主要目的是保护被告人的权益。这本手册1997年被曝光，它对审讯技术的发展影响深远，CIA1983年的《人力资源利用训练手册》和美国陆军的审讯手册都主要是《库巴克手册》的内容。如果审讯有一本圣经，那么就是《库巴克手册》。<br />
　　这本手册引述了国家精神健康学院在1954年的一项研究（这项研究也是由约翰．利里主持的），试验的结果总结出下：<br />
　　结果证实了早前的发现：1，剥夺感官刺激会产生压力；2，对大多数实验者来说，压力会变得无法忍受；3，实验者越来越渴求物理和社会刺激；4，有些实验者越来越脱离现实，把注意力集中在自己内部，产生错觉、幻觉和其它病态结果。<br />
　　但这些结果并非对每个人来说都是麻烦，一个人的痛苦可能是另一个人的意识改变体验。有些人发现他们喜欢感觉剥夺箱子，确实，后来人们只有花钱才能进入这种箱子。利里喜欢给自己注射LSD，后来又把自己关进箱子里。在加拿大，一名科学家把一个52岁的妇女关进感觉剥夺房间35天，但她从来也没有主动要求出来。<br />
　　所有这些实验所证明的一件事就是：不管用什么药物和方法，结果对于人和人来讲都是不一样的。因此审讯的另一重要方面就包括定义某些主要的个性特征，揭示何种方法对何种个性最有效。这些特征都很粗略，显得比较可笑，《库巴卡手册》列出的有“规律顽固型”、“贪婪渴求型”和“焦虑自我中心型”等。<br />
　　审讯者比药物重要<br />
　　在审讯中一直有效的看来还是审讯者，而且有些审讯者做的很好。“你想要一个好审讯者吗？”纽约警察局传奇的审讯者杰里．乔吉奥问道，“给我一个喜欢别人并且受人喜欢的人，给我一个知道怎样能使人感觉放松的人。因为人们越舒适，就越能说话，而他们说的越多，麻烦也就越多，于是想撒谎也就越难。”<br />
　　尽管科学不断进步，但审讯仍然是一门艺术，而非科学。与其他被审讯者一样，穆罕默德对他的审讯者来说仍然是一个独特问题，他是一个世界范围秘密网络的关键结点，在他的头脑中，有着一张潜在的世界圣战地图。如果能让他开口说话，或者只是透露几个秘密，就将是巨大的情报胜利。<br />
　　什么能让人开口　　比尔．卡万在越南打过3年半的仗，当时是一名年轻的陆战队上尉，被指派到西贡南部的一个特别区。在这里，连绵不绝的泥潭齐腰深，有着成群的蚊子和其它咬人昆虫、蛇、鳄鱼和腐烂的红树林。这里被湄公河三角洲的盐水河所分割，偶而有一些开阔的农田。陆战队员们知道在这里有几支越共部队。<br />
　　因此当他抓获一名越共士兵，而这名士兵知道要对他们进行的伏击，并能带他们找到游击队，但拒绝开口时，卡万的手下就用鳄鱼夹把电线连到被捕者的阴囊上，接通110伏发电机。“这一招如同魔法一样有用，”卡万说，“每当要扭动开关时，他就会开口。我们从来没扭动过开关，只是清楚的表明要用电刑。让他们开口的是恐惧而非痛苦。”<br />
　　让人恐惧胜拷打<br />
　　恐惧有作用，它比任何药物、策略或拷打设备更有效。据《卡巴克手册》引述的未指名的科学研究，大多数人实际应付痛苦的能力要高于他们想象，当人们对痛苦比较熟悉后，就会适应它。那些比其他人在身体上承受更多痛苦的人（如从小就频繁挨打的人或患有痛苦的疾病的人）会适应这种痛苦，对它不再那么害怕。因此一旦审讯者采取实际的拷打，他们就失利了。“威胁使用强迫手段通常比实际运用这种手段更能有效地削弱或破坏抵抗，”手册说。<br />
　　威胁制造痛苦能引发恐惧，它比立即产生的痛苦感觉产生的破坏力更大……只要持续时间足够长，对任何不明确或未知事物的强烈恐惧会诱导出心理上的回归，不过如果把这种恐惧物质化，施加某种形式的刑罚，就会减轻痛苦。对象发现他能坚持住，那么他的反抗就会得到加强。<br />
　　此外，如果因其它方法失效后而对某个囚犯施加刑罚，就预示审讯过程可能结束了。“如果他顶住这一最后进攻，那么他将判定自己会获胜，会重新得到自由，”手册结论道。即使剧烈的痛苦的确能获取情报，这种情报很可能是假的，这对那些目的是得到情报而非招供的审讯者来说是个棘手的问题。很多有用的情报时间性很强，取得假线索或抓捕无辜者只会浪费时间。<br />
　　不用死威胁囚犯<br />
　　手册劝阻审讯者不要用死来威胁囚犯。作为一种策略，“死亡威胁不仅无用，实际还会更糟，”手册说，因为死亡带来的绝望感会使对象陷入消沉，在某些案例中，囚犯们会把死亡看做摆脱困境的一条荣誉道路。<br />
　　不过有人表示反对。“我告诉你怎样让人开口说话，”一名退役特种部队官员说，“朝他左右开几枪，那么你想让他闭嘴都不能了。”从他自己的经历判断，对审讯者来说，死亡威胁对于撬开意志坚定的对象的嘴可能有用，但它和痛苦一样，最为重要的是恐惧，一个无惧的囚犯不会成为告密者。<br />
　　科比的经验之谈<br />
　　现代审讯者如果有原型的话，就应该是迈克．科比，他是以色列安全总局“沙巴克”的前主审讯官。科比是世界上审讯敌对阿拉伯囚犯经验最多的人，在这些囚犯中，有些人是恐怖分子或者狂热的宗教极端分子，科比说这些人“对犹太人仇恨的程度是不可理喻的”。科比如今60多岁了，同被捕者打了几十年交道，对他们用尽了哄骗、欺诈、伤害、威胁和详察，持续施加压力，寻找突破口。<br />
　　如今他从沙巴克退休了，有了3个外孙。他很为自己的技巧骄傲，其中一点就是能说一口流利的阿拉伯语，他是由于热爱语言才成为审讯官的，从小就能讲希伯来语、依地语和阿拉伯语，并且在高中学习过阿拉伯语，精通其方言和俚语。他还拥有解读身体语言和面部表情的诀窍，能够察觉到谎言。他是个娴熟的演员，能够对囚犯交替展现友好或恫吓，有时态度能立即发生一百八十度转变。科比说他很少用暴力强迫囚犯招供，在大多数案例中，暴力并不必要。<br />
　　“人入狱后就会改变，”科比说，“他们在外面可能是英雄，但在监狱里就不一样了。环境变了，人们对未知的东西感到恐惧，他们害怕受到拷打，害怕被关押的时间太长。试试当你被蒙上头坐上4个小时，而且又饿又累又怕，被从一切事物中孤立开来，对于要发生什么没有一点概念，那时你会怎么样？”当被捕者相信拷打、处决、无限期关押甚至所爱的人被迫害等任何情况都有可能发生时，审讯者就能开始工作了。<br />
　　不管被捕前他是什么人，入狱后其自我感觉都会模糊。孤立、恐惧和剥夺感会迫使人退缩，从而不得不重新定位自己，重新安排需要优先考虑的事。科比说，对于大多数人来讲，压力之下忠诚的层次是：1，自我；2，组织；3，家庭；4，朋友。换句话说，只要压力足够大，即使最坚强的恐怖分子（极少有例外）也会作出保留，为了保护自己不惜以任何人或任何事为代价。<br />
　　对中年囚犯来说家庭常常压倒组织（或事业）成为第二重要的忠诚对象。年轻人一般有着强烈的效忠和雄心，但对老人来说，即使那些极其坚定、被下属敬仰和效仿的老人也有着更有价值的爱和责任。年龄会磨损理想主义，降低热情，并冲淡残忍的性情，抽象的概念让位于妻子、孩子和孙子。“注意到没有，哈马斯领导人不会让自己的儿女或孙子充当人肉炸弹，”科比说。<br />
　　找受审者的弱点<br />
　　对科比来说，找到弱点的三个关键因素是：准备、调查和表演。<br />
　　为审讯囚犯做准备就意味着先让他软化起来。完美的情况应该是像穆罕默德那样，在凌晨把他从睡梦中拉起来，动作要粗暴，把他绑起来蒙上头（最好是用粗糙肮脏的臭布袋），让他浑身不舒服地等候，可能还要把他的衣服扒光弄到一个冰冷潮湿的房间中，强迫他以不舒服的姿势站着或坐着。在审讯之前，可能要让他连续数日不睡觉，孤立起来并不给饭吃。他可能不知道自己在哪里，也不知道什么时间日期，不知道被关押多久了。如果他受了伤，不会给他止痛药。<br />
　　一个名叫穆萨．库里的巴勒斯坦商人对这种操练太熟悉了，这个34岁的男子长着黑胡须，头发稀薄，他对以色列的占领和他是监禁经历感到痛苦，曾被以色列军队逮捕审讯过6次，其中一次被关押了71天。<br />
　　“我的双手被铐到身后，头被一只马铃薯袋子蒙住，”他说，“我的腿被铐在一个小椅子上，椅子腿只有10到20厘米高。椅子是硬木做的，前腿短后腿长，因此你不得不身体前倾，只有手还在后面。如果你坐下来，椅背会戳到脊梁上，脊梁向前弯，手就会被吊起来。很痛苦。你高声要求几百次他们才会带你去洗手间。”他想到的只是一件事：怎样才能结束这种待遇。“你的脑子在来来回回来来回回地想，不可能还有清醒的意识，”他说。<br />
　　准备审讯意味着要预先掌握审讯对象的每一点信息。美军陆军审讯手册建议，如果对对方一点也不了解，也要准备一大沓“假文件”，意在向审讯对象表明，自己知道的要比说的多。没有什么能比把审讯对象所认为的秘密或不引人注目的东西呈现在他面前更让他慌乱的了，它使审讯者看上去更有力，更显得无所不知。如果一个人对重要的感觉受到打击，他就会放慢撒谎的速度，因为他会认为可能被戳穿。<br />
　　这种信息碎片有很多，用老式的调查或审讯对象的同伙都能得到，聪明的审讯者能够有效地利用这些信息碎片。这种碎片可能只是被审讯者的兄弟姐妹或者关键同伙的名字、他女朋友的名字或是对他的组织来说有着特殊意义的词句。了解专有的细节能消除一个秘密社会的神秘感，一个人如果加入这种组织，就会觉得独特、重要和出众，他甚至会给日常活动也赋予特殊意义。一名洞察这种秘密社会的审讯者要是把它的共同语言、文化、历史、习俗、计划和等级次序拆穿，最坚定的信仰者也会动摇。如果被审讯者怀疑一个信任的同志（或者他本人）背叛了组织，那么他的秘密共有的目标和使命感就会受到破坏。有个熟练的审讯者即使只掌握一点重要信息，也能使被审讯者对自己下定决心坚决不说的情报的价值产生怀疑。一个人可能会不惜一切保护某个秘密，但当秘密不成秘密，就是另一回事了。这就是一个掌握丰富信息的审讯者如何突破一个组织的防线的。<br />
　　要懂囚犯的语言<br />
　　科比相信，对审讯者来说最重要的技巧是懂得囚犯的语言，通过翻译审讯会带来灾祸。语言是所有社会联系的根基，在“哈马斯”和“基地”这样的秘密社会中有着关键作用。一个共有和词或暗号有助于巩固整个组织。<br />
　　“我设法确立一种印象：我说你的母语比你自己还好，”科比说，“没有口音，没有语法错误，我像一个最好的朋友一样跟他交谈。我可能会问一个关系某个单词、句子或表达的问题，问在他的文化中怎么用它，然后表现出我知道的比他要多，这会使他感到非常尴尬。”<br />
　　宗教极端分子是最难对付的，他们沉浸于自己的空间内，进行自我催眠。他们通常受过良好教育，经济生活和感情生活都不错。他们过着苦行生活，鄙视和他们信仰不一样的人。他们在脑力和体力上都很强壮，不会被有形事物所影响。通常对他们来说，事业的正确性无与伦比，因此他们为之可以做出任何事。然而科比说，只要进行充分的监禁，大多数极端分子最终也会被突破，但还有些极硬分子能顶得住。<br />
　　穆萨．库里说，他虽然受过6次审讯，但从没有向审讯者屈服。科比不知道库里的案例，但他狡猾地笑着说，“如果某人对你说，他被我们捉住但从未进行过合作，你可以打赌他在撒谎。在有些案例中，那些以坚强而著称的人对我们最为有助。”<br />
　　审讯也布置舞台<br />
　　审讯也非常有戏剧性，《库巴克手册》特别提到布置舞台。<br />
　　进行审讯的房间要没有能扰乱注意力的东西，墙壁、天花板、地毯和家俱的颜色不能让人吃惊，不要挂图画，如果要挂的话其色彩应该是阴暗色调。要不要桌子并非视审讯者的需要而定，而是根据受审者对优越和法定的内涵的预期反应确定。一张普通的桌子较为可取，给被审讯者一个加了厚垫的椅子有时比用直背的木椅要好，因为他已经站了好长时间或者被弄得很不舒服，受到相反的待遇会使其更加迷惑。<br />
　　手册还建议用强光照射受审者的脸，而审讯人员要待在阴影中。在房间里没有电话或者任何与外界联系的东西，以强化集中感和对象的禁闭感。在科比的经历中，有时同事在另一个房间房间大声的进行拷打也有用。据退役特工比尔．韦格纳说，老式的CIA审讯训练建议在审讯室外进行假处决。<br />
　　审讯者是个骗子<br />
　　一个好的审讯者是个骗子，科比的一个花招就是让20多个刚捕获的、头被蒙上、极不舒服、饥饿恐惧的人在走廊里站成一排等候审讯，科比走到他们前面，威风凛凛地喊到：“好，谁想跟我合作？”即使没有人举手，或者只有一个人举手，他也会说，“好，很好，你们8个。我从你们开始，其他人在这里等着。”如果被审者相信别人也屈服了，他就容易屈服。通常耍了这个花招后，很多人就会合作。人是群体动物，更喜欢随大流，特别是进行急转弯时。<br />
　　有一次科比得到情报说，有两个他正审讯的人是一个恐怖组织的秘密成员，知道将要进行的袭击。这两个人都是坚强的农夫，很难被恐吓和压迫。科比单独审讯他们几个小时，对每一个人，他都以友好的提问开始，然后变得越来越愤怒，指责受审者不说实话。他会侮辱他，把他踹下椅子，让警卫作势要打他，再把警卫推开。然后他会让受审者坐回椅子上，给他一只雪茄，并点上火。“让他看看两种氛围的不同，敌对的和友好的，”科比说。但两个人都没有动摇。<br />
　　最后科比设了一个圈套，他向一个人宣布审讯已经结束，这个人的同伙被蒙上头坐在屋子外面的走廊里。“我们马上就放你，”科比说，“对你的合作我们感到很高兴，但你首先必须为我做点事。我会问你一系列问题，这只是例行手续，我需要你大声清楚地说‘是’，好让录音机录下来。”然后外面的蒙面人会听到一个很大的声音，但他听不清说的究竟是什么。科比读出一长串问题，回顾的只是受审者的姓名、年龄、婚姻状况、被捕时间、关押天数等等，这些问题不时被囚犯大声和合作的“是”所打断。这个花招足以向另一个人表明，他的朋友已经屈服了。<br />
　　科比释放了头一个人，把第二个人带进来。“我不需要再审问你了，”科比说，“你的朋友全都向我供认了。”他递给第二个人一支雪茄，并给他一顿好吃的，告诉他其朋友提供的信息足够让他们在监狱里呆一辈子……除非他也能说出某些让法院能从轻发落的事。第二个人深信他的朋友已经背叛了，他迅速保护自己。“如果你要想挽救以色列人的生命，那就赶快行动，”他告诉科比，“我的朋友们正乘车前往耶什瓦内哈利姆（一所宗教学校），他们要绑架一些学生……”以色列在厄里兹找到这些人，行动流产了。<br />
困惑技术<br />
　　然后就是“爱丽丝漫游仙境”了。<br />
　　爱丽丝漫游仙境或者称困惑技术的目的是混淆受审者的期望和条件反射……困惑技术的构想不仅要消除熟悉的事物，还要用意料之外的东西代替它……有时在同一时间问两个或更多的问题，审讯者嗓音的音质、声调和音量与问题的重要程度无关，不能形成提问和回答的范式，也不能让各种问题有逻辑关系。<br />
　　手册说，如果耐心的应用这种技术，对象就会“为了制止喋喋不休的质问他的话”而开始说话。<br />
　　“好警察/坏警察”无疑是最著名的套路，一个审讯者扮演黑脸，另一个唱红脸。还有鲜为人知但同样有效的技术就是“骄傲和自负”、“自负和自尊”或（如自命不凡的《库巴克手册》所说的）“斯宾诺莎和莫蒂默．斯耐德”，在“自尊”部分，审讯者一再有意向对象提出其回答不出的问题，对象不断受到严厉指责和威胁（“你竟然不会回答这个问题？”），并宣称他不说实话，直到好长时间以后，问一个他能回答出来的简单问题。一名领教过这种技术的美国战俘说，“现在我知道这很奇怪，但当他们转到一个我了解的问题时，我的确很感激他们。”<br />
　　CIA的心理学家试图建立一个审讯的基本理论，即强迫手段会使个性逐渐衰退，但这一理论不能令人心悦诚服。审讯只是把一个人逼到墙角，被迫做出艰难选择，摇摆于不可靠的摆脱途径。<br />
　　无意识的招供<br />
　　科比频繁的耍计谋，他的审讯对象永远不知道他们的审讯何时真正结束。在讯问之后，囚犯们通常会在正规的监狱里呆一段时间。以色列人对监狱进行监听，窃听装置伪装的很好，但又有意弄得容易被囚犯们发现。于是囚犯们就会相信，监狱里只有某些部分受到监听，但实际上监狱的所有地方都有窃听器。囚犯们在任何地方的谈话都能被偷听到，它们是情报来源的无价之宝，那些在高强度的审讯中坚持下来的囚犯们在审讯结束后，常常在与狱友的谈话中降低警惕性。<br />
　　为了延长这种无意识的招供，科比还有另一张牌打。在进行数周的严厉讯问后，只要一名受过审的囚犯被放回普通监狱，他就会受到巴勒斯坦同道的欢迎，这些人把他当作朋友，祝贺他在持久的审讯中坚持下来。他会被当作英雄，受到照料和赞扬。他所不知道的是这些快乐的新同志为科比工作。<br />
　　科比称他们为“小鸟”。他们是巴勒斯坦人，因受到某些刺激而同意与沙巴克合作。在欢迎新囚犯加入到他们中来的数天或数周后，他们将开始问一些问题。他们会听取这名囚犯谈论审讯过程，他们会说，“要让外界的人了解你对以色列人说了什么、没说什么，这非常重要。告诉我们，我们会把信息传递给外界需要了解的人。”甚至那些设法对科比保守秘密的人也会成为“小鸟”。<br />
　　“好警察”的技巧<br />
　　大多数职业审讯者工作时得不到CIA、FBI或反恐战中的军队那种自由。警察必须向其对象宣读“米兰达警告”，他们如今要是进行身体威胁或虐待嫌疑人，自己就会进监狱。传奇的纽约警察局审讯官杰里．乔吉奥在这些规则之下工作近40年了，他可能不知道CIA和军方技术的名称，但大多数他可能在工作中都见识过。乔吉奥现在是纽约县公诉人，是个大嗓门的大个子，头发灰白，肚子很大，有着一双又大又机警的绿褐色眼睛。他被以前的同僚们视为巫师，“我们这一代人都出身于杰里．乔吉奥的审讯学校，”一名最近退休的曼哈顿谋杀案侦探约翰．伯格斯说。<br />
　　“每个人都知道好警察/坏警察套路，对吧？”乔吉奥说，“好，我一直是好警察，也不和坏警察一块工作，我不需要他。你想知道为什么吗？答案就是每个人在内心深处都有述说自己有事的愿望，这一点很重要。真的，不管这样会对他们造成多大伤害，不管保持沉默对他们来说是多么重要，他们还是想说自己的故事。我告诉他们，‘嗨，我知道你做了什么，我会证明给你看，现在你要怎么做？如果你表现出自责，如果你帮助我，我会保护你，’我对他们这样说。如果你给他们一个原因，他们就会告诉你任何事。”<br />
　　“请君入瓮”<br />
　　最重要的事是让他们开口，最顽固的对象是那种拒不开口并立即要求律师的人。乔吉奥相信只要他能让嫌疑人开口，语言的洪流最终会带出真相。一个杀人犯一天之内给了他好几个自愿声明，每一个都签名了，每一个都不相同，每一个都有点接近真相。<br />
　　这个杀人犯是卡洛斯．马丁内斯，他是个笨重的前橄榄球选手，1992年5月份杀害了自己的女朋友切丽尔．玛丽亚．怀特，并把她的尸体扔在纽约。因为很多年轻女性受害者都是被她们的男友杀害的，乔吉奥开始寻找怀特的男友。马丁内斯听说乔吉奥想找他问些问题后，就给这位侦探打电话。乔吉奥有怀特与马丁内斯的合影，在所有的照片中，这位年轻的情郎的大脑袋上都是卷发，但出现在乔吉奥办公室的马丁内斯却是光头。这名侦探立刻产生了怀疑，那些犯了罪担心别人看到的人常常试图改变自己的外貌。<br />
　　以下是乔吉奥总结的很长却富有成果的谈话：“昨夜我在家，”马丁内斯说，“她确实给我打电话了。”“真的吗，为什么？”“她想要我开车接她，‘我在看大都会队的棒球比赛，我不能开车接你。’”<br />
　　就是了。乔吉奥假装对这个声明感到很高兴，他谢过马丁内斯，把它写了下来，然后让这个年轻人签字。马丁内斯签了。<br />
　　然后乔吉奥凝视着这个声明，并古怪的看了马丁内斯一眼。“你知道，卡洛斯，这个声明对我来说有点不对头。你们俩的关系确定多长了？7年？她给你打电话要你夜里去接她，当时她才下班，那个地方不安全，而你对她说不？你的意思是说一场电视转播的球赛对你来说更重要？”<br />
　　这个问题很巧妙。侦探知道，马丁内斯想给他留下一个好印象，不想让他头脑里存有任何未定问题，因此他对第一个声明听上去不圆满很是关注。乔吉奥的问题还触及到马丁内斯的骑士精神，这对很多西班牙男子来说是个重要素质。不能表现的太不绅士，这是一个刚被残忍杀害的年轻女子，如果他承认她曾经打电话要他来接，但他为了看电视比赛而没去，使她被杀害，那么她的家人和朋友会怎么看？这个问题还巧妙的暗示：那个地方不安全，人们一直在那个地方受到殴打甚或杀害。或许马丁内斯能够承认他在切丽尔被害那晚曾见过她，而不把自己直接牵涉进去。他立刻咬上了乔吉奥的诱饵。<br />
　　他说，“杰里，我告诉你到底发生了什么吧。”（“注意，”乔吉奥骄傲地说，“他已经开始叫杰里！”）马丁内斯现在说下班后去接怀特，但他们吵了一架。“她对我非常愤怒，告诉我她不需要乘我的车，于是我一直等她上了巴士才离开。”（“看，现在他把自己描述成一个骑士！”乔吉奥愉快地说。）<br />
　　“让我记下来，”乔吉奥说，他又一次假装对这个声明感到高兴。他整洁的把它写下来，要马丁内斯看过之后签字。马丁内斯又签了。<br />
　　乔吉奥又一次斜视着这个声明，“你知道，卡洛斯，有些东西还是不对头。切丽尔是个非常漂亮的女孩子，人们看到她就会记住她，她曾经多次在夜里乘坐巴士回家，同车的人都知道她是谁。而你知道吗？那天晚上没有人在巴士上见过她。”<br />
　　用乔吉奥的话来说，这是“纯粹的屁话”。他从未和任何坐过那辆巴士的人谈过话。（“有时你得冒冒险，”他说。）<br />
　　马丁内斯又一次慌乱起来，他没能让这位侦探打消怀疑。于是他又进行试验。“好，好，”他说，“这才是真实情况，我告诉你到底发生了什么吧。切丽尔打电话过来，我开车接她，但路上我碰到一个朋友，我不能说出他的名字，我们一起去接切丽尔，然后我和她吵起了架，吵的很凶。我的朋友感到很厌烦，于是我们开车走了，来到百老汇第181街道，把车停在一家麦当劳旁边。我的朋友拿出一把手枪，要我下车。‘在这儿等着，’他告诉我，‘我去把你的问题解决掉。’然后他走了，我在那里等着，不久他回来了，说已经给我解决掉问题了。”<br />
　　乔吉奥高兴的点点头，开始把第3个声明写下来，他假装对马丁内斯拒绝透露那位朋友的名字感到奇怪，于是这个年轻人迅速供出一个名字。乔吉奥的助手一直在通过单向玻璃窗观看审讯过程，他立刻调查马丁内斯的这个朋友。当第3个声明写好签了字并整齐的放在另两个声明之上后，乔吉奥向马丁内斯提出了一个新问题：好像他的这个朋友住在南加利福尼亚，而且住的有一段时间了。“第四个声明我们没有完成，”乔吉奥说，“马丁内斯的家人雇了个律师，他要求警察局禁止我们进一步讯问他的委托人。”当然，这时已经太晚了。<br />
　　刑讯逼供　　美国政府公开宣称反对刑讯，但只要环境许可，美国武装部队和间谍机构就会精神抖擞的运用高压手段。不过美军和CIA在其关于使用高压手段的出版物里都很坦白，《库巴克手册》只在少数几个地方用了“外部技术”等非常婉转的说法。<br />
　　没有人体验过比凯斯．豪尔更有戏剧性的效果，他被CIA解雇之前赢得过一个“嘎吱船长”的绰号，现在他称自己为“政治正确的模范”。他是个好斗的好人，曾经参加过海军陆战队，退伍后获得过历史和国际关系硕士学位，后来当了警官，因为他渴望物理性的刺激而做学术。他的绰号就是这样来的。<br />
　　1979年，当他申请加入CIA后，立即就被录用了。他被派到调查和分析部门，在那里他成为CIA的秘密工作者，这是个相对很少的群体，豪尔说“大约只有48个人”，被称为“指节铜套”。<br />
　　豪尔喜欢旅行、训练和吹牛，即使他感觉得到局里其他人看不起他这样的人。当美国驻贝鲁特大使馆1983年4月18日被炸后，需要进行调查，但CIA想派一个自己人去，豪尔被选中了。<br />
　　他睡在贝鲁特的屋顶，每两晚就换一个地方。当时美国人在那里很危险，更不用提他的CIA官员身份了。<br />
　　他说他毫不犹豫的用残忍手段审讯黎巴嫩人，用棍棒和橡皮管抽打嫌疑人，或者把他们泡在水里用电击。通过使用这种手段，他最终找到了一个涉嫌制造大使馆爆炸案的“主子”，这个人叫埃利阿斯．尼姆尔。“他是我们最大的战果，”豪尔说，这个人关系非常广，“当我把这个消息告诉黎巴嫩国防部长时，我看到他的脸一下子全变白了。”<br />
　　尼姆尔28岁，很胖，习惯过着舒服日子，是个富有、悠闲和有权的年轻人。他穿着宽松的裤子、闪亮的运动衫和古奇鞋来到警察局。当他进入大楼后，豪尔说一些警官“试图躲开”，因为他们害怕受到报复。尼姆尔在头一次会见中很冷淡，而且很得意的笑。他确信只要家里人和亲戚知道消息，马上就会得到自由。<br />
　　当豪尔有机会跟他说话时，开始要他醒悟过来。“我是美国情报官员，”他说，“你的确认为自己不知道要炸我们的大使馆，而且我们不能把你怎么样，是吗？你确实应该好好想想，告诉自己跟我谈话是个好主意。要想走，最好的方法是……我知道你认为自己几分钟后就能走了，这是不可能的。你在我手上，我才是决定要拿你怎么办的人。你要想保命，惟一能做的就是合作。”尼姆尔不屑地朝他笑笑。<br />
　　第二次他们会面时，尼姆尔状况就不大妙了。他的亲戚对他无能为力，没人打过他，但他被罚了两天的站。豪尔把他放在一张直背金属椅前，用热辣辣的强光照射他的脸。这位特工坐在光亮后面，因此尼姆尔看不到他。尼姆尔不那么骄傲了，但依旧保持沉默。<br />
　　在第三次审讯中，豪尔说，他把尼姆尔踢下椅子。这是第一次有人打他，他看来被吓晕了，只是瞪着豪尔。自从几天前被逮捕，尼姆尔就没吃过饭，但他还是什么都不说。<br />
　　“我把他送回牢笼，让他坐在一个大电扇前，用水一遍遍的浇他，冻了他24个小时。当他回复过来后，你能看得出他的情绪变了。他没能出狱，渐渐知道没有人能救他。”<br />
　　此后10天，豪尔继续给他施加压力。在审讯过程中，他又把尼姆尔踢下椅子，而且他和协助他的黎巴嫩警官有时用木棍打尼姆尔的小腿。最终尼姆尔屈服了，据豪尔称，他招认了自己在爆炸和刺杀黎巴嫩总统的行动中扮演的角色，他说计划的背后是叙利亚情报人员（并非每一个人都赞同豪尔的解说）。<br />
　　不久尼姆尔死于狱中，豪尔听到这个消息时正在华盛顿。他估计是被人谋杀的，以防供出其他卷入这一事件的人的名字。豪尔带着尼姆尔招供的录音带，认为已经完成使命，但完成报告几个月之后，他被解雇了。据他了解，CIA特工刑讯逼供的事情流传开了，美国政府感到非常尴尬。<br />
　　因这次爆炸受到指控的人没有一个被处决，豪尔相信美国为他的调查付出了惨重代价，不得不大事化小，小事化了。豪尔的站长威廉．巴克利此后被绑架，受到严刑拷打，最后被杀害</div>

							
							
							
							
