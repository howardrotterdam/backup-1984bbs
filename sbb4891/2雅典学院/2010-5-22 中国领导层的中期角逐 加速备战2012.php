然，要说政治局及其常委会的这些单个竞争者的政治前景已经确定，还为时过早。另外需要指出的一点是权力从胡温向习李的转移也不是一个必然的结论。中国政治中也会有黑马出现，正如美国政治一样。然而，正是基于这个原因，我们应该多关注更广泛群体中的潜在权力竞争者，尤其是省级领导层中的升起的新星。还有一点也很重要，即将到来的18届党代会的人事变动将标志着所谓第五代国家最高领导集体的全面掌权。正如中华人民共和国历史一再显示的，每一代领导人都将带来可以清楚感知的统治方式、不同的施政方法以及特殊的热点议题。了解中国未来第五代和第六代领导人的集体个性或代际特征，尤其是未来领导人最重要的补充来源――省级负责人们，是一个与找出最高领导层的有力竞争者同样重要的任务。<br />
