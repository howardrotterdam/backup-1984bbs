四川省委书记刘奇葆和山东省委书记姜异康在国家两个最重要的省份担任一把手。两人都有丰富的中央政府内部的领导经验，他们都属于下届政治局候选人中的领先者。在过去的30年里，北京或上海的市长通常会进入政治局，有时甚至成为政治局常委。郭金龙在省级领导层的坚实经历以及韩正长期管理这个国家最大城市的经历都是他们非常有价值的资本。但他们要获得下届政治局的席位，今后两年内都需要转任各自城市的党委书记或者转到其他重要领导位置。卢展工、孙春兰和王珉都是与前面提到的胡春华、孙政才一起，在2009年11月获任现有职务。这一职务变动使他们在加速谋求政治局席位时占得先机。正如本文前已论及，在即将到来的政治角逐中，每个人都有他或她清晰的优势。 <br />
 <br />
2012年中国领导层的主要竞争者已经开始进行个人的竞选活动（当然是以中国的方式）。由于可以理解的原因，国家副主席习近平和常务副总理李克强可能还没有显示出他们与前任有何不同，直到他们完全掌权以后。但是，已有迹象表明习和李都代表了新的领导方式，愿意寻求新的政策优先顺序。例如，在2010年春节期间，习通过短信息服务（SMS）发出一条文字信息，向中共近100万基层组织的官员们表达他“个人的”祝福，这是一种党的最高级领导人和地方官员交流的前所未有的方式。28同时，李也以其对新问题如气候变化、能源效率、医疗保健、可负担的住房等的强烈关注而引人瞩目，所有这四个问题10年前对于中国领导人都不是优先考虑之列。<br />
 <br />
汪洋和薄熙来都以敢言著称，人送绰号“两个大炮”。从2007年被任命为广东省委书记开始，汪洋就一直提倡一种新的中国经济增长方式（或模式），并坚持政治改革的必要性。他个人发起了一波新的“思想解放”，要求地方官员克服意识形态和政治禁忌。在2008年的一次省委会议上，汪洋引用邓小平对经济改革面临的巨大困难的描述，说政治改革可能是一条“血路”。29年初，中国官方媒体广泛转载一篇网络流传的写汪洋的长文，该文最初发表于人民日报所属的《大地》周刊。30这篇文章强调汪出身低微，更重要的是，该文重点提出是邓小平“发现”的汪洋，1992年邓视察安徽，遇到了这个37岁的铜陵市委书记。文章引用邓的话说：“汪洋是个难得的人才。”文章结尾预测，作为一个拥有“鲜明的团系色彩”的政治局委员，未来汪洋将有更光明的政治前景。在中国的政治和媒体文化中，对一个省级负责人如此报道实属罕见。 <br />
<br />
薄熙来的“自我推销运动”（self-promotion campaign）引起了更为广泛的关注。现在他以两个独特的行动而著称：“打黑唱红”。 这个口号的上半部分指薄去年在重庆发起的一次警方严打运动，逮捕了他所说的“地下黑社会匪帮”，这些人通常有腐败的执法官员保护。在薄从辽宁带来的亲信（现任重庆公安局局长）的帮助下，薄发动了这个城市的3万名警力，逮捕了许多“黑社会成员”和腐败官员。薄把前任公安局长（也曾任该市政府司法局长）送上审判庭。根据《重庆晚报》的消息，薄指示该市警察的目标是逮捕该市的大约9000名罪犯。31 <br />
 <br />
至于“唱红色歌曲”，薄要求重庆的官员和普通居民唱革命歌曲以振奋精神。正如很多国内外记者所看到的，这是薄熙来展示其太子党家谱的一种方法，使他成为他父亲那一代所建立的红色政权的理想的继承人。32同时，薄熙来向500万退休老干部、残疾老兵和城市贫民派发了总值6亿元的“红包”。33尽管有批评断定这些办法反映了薄熙来的“文革式的思想和行为”，薄却似乎在重庆公众中大受欢迎。在全国，薄的高调令他赢得了人民日报发起的2009年网络评选“年度人物”称号。34 <br />
 <br />
然而，其他高层竞争者仍保留着一种更为传统的、不那么张扬的领导风格。例如，天津市委书记张高丽最近对国外人士说，他更喜欢“脚踏实地、真抓实干”的风格，他的座右铭是：“多干事，少说话。”35上面讨论的所有这些行为――有些很新鲜，更多的很熟悉――反映了中国精英政治中的活力变化。<br />
 <br />
在痛苦的――但也很有和平希望的――政治转变中，这个国家很快将见证更有活力、可能更“两党化”（bipartisan）的阶段。当一个新的领导层出现的时候，巨变似乎不可避免，上面描绘的省级领导人几乎肯定站在这些变化的最前沿。围绕即将到来的领导层更替，总体上的不确定性以及它将对中共的派系动态造成的深刻影响，标志着2012年将可能是中国精英政治中极为有趣的年份。本专题关注通往18大道路上发生的政治角逐，后续的其他部分将考察一系列其他可能影响该事件结果的因素，并预测它可能会如何重新定位中国近期和长期的政治前途。<br />
