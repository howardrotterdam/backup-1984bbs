2012年政治局席位的省级有力竞争者<br />
 <br />
中国的31个省级行政区级别相同，但它们并不平等。某些行政区在国家的社会经济和政治事务中比其他区承担更多的份量。对于62个省级负责人情况也是如此，他们中的一些人比其他人更有权势，影响更大。通常，省委书记在图腾柱上的位置比省长高，担任政治局委员的6个省级负责人毫无疑问是当中最有权势的。直辖市――北京、天津、上海以及上一个十年加入进来的重庆――的市委书记按惯例在政治局中据有一席之地。中国最富有的省份广东同样如此，现任省委书记汪洋和之前的三个前任――谢飞、李长春、张德江――在任职省委书记期间都进入了政治局。25在过去的20年间，这种情况在山东、江苏、浙江、福建、辽宁、河南、湖北、四川和新疆也在增加。例如，4个现任政治局委员――贾庆林、习近平、贺国强和王兆国――之前都曾担任福建省负责人。如果省级负责人职位是未来提拔的进阶石，在上述省或市任职基本上可以视为政治局委员的入场券。<br />
 <br />
表9（见下页）列出了这些现任省级负责人中最有可能进入下届政治局及其常委会的候选者。这份清单根据3个原则而提出：个人的年龄；他们现在任职的省市；他们基于当前中国领导政治权力平衡规则的派系归属。后邓时代的中国精英政治可以从广义上概括成两个主要联盟的争斗：“平民同盟”和“精英同盟”。26 这两个联盟因其成员的家庭背景、职业道路、政治联系而有所区别。他们代表了不同的地理区域（内陆对阵沿海），而且某种程度上也代表国家不同社会经济群体的利益。民粹主义联盟的核心是上面讨论过的团派，而精英主义联盟主要由太子党和上海帮成员组成。<br />
<br />
在这13个领导人中，11人现任省（市）委书记，其余两人北京市市长郭金龙和上海市市长韩正之前有过担任省或市委书记的经历。郭曾于2004-2007间年担任安徽省委书记，2000-2004年间担任西藏区委书记。韩曾在2006年末、2007年初担任过几个月的上海市委代理书记。在列出的13个人中，4个是现任政治局委员。汪洋、薄熙来和张高丽可能在新政治局中保留他们的席位。他们三人都拥有很好的机会成为政治局常委。由于汪洋年纪相对较轻，他将可能在2012年后担任两届任期的常委。这将有可能使汪在未来中国领导人排名中成为极少数最具政治份量的人物。<br />
 <br />
如果下届中央委员会委员的出生年份上限是1945年，上海市委书记俞正声将能够保留他在下届政治局中的席位。考虑到他已经担任两届政治局委员，他极有可能获得一个政治局常委席位。俞的老资格令他成为一个关键的政治人物，类似于15届政治局常委中的李瑞环或者16届政治局常委中的曾庆红。然而，由于俞潜藏的巨大权势，胡锦涛和其他现任的高层人物可能试图在18大上协谋让他退休。<br />
 <br />
胡春华和孙政才都生于1963年，他们被中国官方媒体广为报道，被视为所谓第六代领导人中冉冉升起的新星。过去20年来，从1980年代末担任西藏共青团的高级官员开始，胡春华的晋升之路大部分应归功于胡锦涛的强力支持。媒体特别提到一个事实，胡春华能够说流利的藏语。27为与胡锦涛对胡春华的庇护相抗衡，贾庆林和曾庆红在孙政才的迅速提拔上起到了关键的作用。看起来两个竞争联盟之间达成了一个政治交易，让这两个更年轻的人进入新政治局。这不仅将保持派系的权力平衡，也可被视为第五代领导集体延伸其连续性的集体努力。<br />
