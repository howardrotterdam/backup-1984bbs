当然也会有一些例外。例如，王珉于1983-1985年在南京航空航天大学获得工学博士学位（全日制）。王也曾在1987-1989年在香港科技大学作访问学者。在62个省级负责人中，只有3人（其中包括王珉）据称曾在海外学习。其余两人包括：专业为经济管理的巴特尔1989-1990年在东京做访问学者，湖北省省长李鸿忠曾参加哈佛大学肯尼迪政府学院的未来领导人培训计划。没有人获得过海外的学位。 <br />
