教育背景<br />
第四代和第五代领导人的主要区别是他们的教育背景。第四代领导人中很少有人接受过研究生教育，1940年代出生的领导人大部分接受的是文革之前工科专业的本科教育。第5代领导人很多接受过研究生教育，通常是在职教育，很多人拥有博士学位。他们的学业领域相当分散，很多人学习经济学、管理学、政治学或者法学。在62个省级负责人中这个新的趋势十分明显。表6说明在2001年有40个领导人（64.5%）拥有高级学位，只有8个省级负责人（13%）参加过研究生教育（通常获得硕士学位）。18 与此相对应的是，现任省级负责人中有8人拥有博士学位，包括广西区委书记郭声琨（1952年生）、陕西省省长袁纯清（1952年生）、天津市市长黄兴国（1954年生）、新任命的青海省省长骆惠宁（1954年生）以及上面提及的王珉和孙政才。 <br />
