一个有趣的发现是有多达21个省长们（68%）曾有过秘书的工作经验，这个统计数字反映出庇护关系在中国精英选拔中的重要角色。某些人职业生涯初期是做前任高级官员的个人助理。例如湖北省长李鸿忠就是前政治局委员李铁映的秘书。湖南省长周强曾经是前司法部长肖扬的秘书。四川省委书记刘奇葆和省长蒋巨峰职业生涯初期都是所在省级政府办公室的秘书（刘在安徽，蒋在浙江）。山东省委书记姜异康升官之路几乎完全是秘书工作。生涯早期做秘书工作多年以后，姜1988年被提拔为中共中央办公厅秘书局副局长。从1995年到2002年，他担任中共中央办公厅副主任，其后被任命为重庆市委副书记。<br />
 <br />
表7也显示，16个省长（52%）有过县级领导层以上中国共青团官员的经历。那些从共青团内获得升迁和1980年代早期直接在胡锦涛手下担任共青团省和国家级领导的人通常被称为“团派”（共青团派系）。本研究也考察了那些1993-1998年间当李克强负责中国共青团之时，担任团的省和国家级领导职位的那些人。但是，有一点很重要，并不是所有这16个有过共青团领导经验的省长们都应被划分为“团派”。有些人从未在担任过共青团省或国家级的领导职位，因而并不符合。上海市市长韩正尽管曾担任上海市团委书记，但并不属于“团派”，他升迁主要是由于和上海帮的关系。<br />
 <br />
然而，这些有共青团经验的省长中，有13人确实属于团派成员。表8（见下页）表明，省级负责人中属于团派的数量，2003年为6人（9.7%），到2005年增加到13人（21%），到2010年增加到21人（33.9%）。团派成员大量占据省级领导层并不令人惊讶。在中共17届中央委员会中，86个领导人（23%），包括正式委员和替补委员，可以被确认为团派。本研究认为胡锦涛和李克强的追随者可能将继续在下届中央委员会和政治局中占据很大一部分席位。<br />
<br />
当然，现在及18大之后，团派领导人无论在省级或国家领导层中都无法完全占据统治地位。其他最高级领导人（前任或现任）的亲信们也在省级领导层中占据重要的位置。北京市委书记刘淇和天津市委书记张高丽被公认是江泽民的亲信。新任命的吉林省委书记孙政才被认为是贾庆林和曾庆红的亲信。具有高干家庭背景的“太子党”领导人经常在省级管理层中与团派争夺重要的职位。上海市委书记俞正声和重庆市委书记薄熙来是省级领导层中“太子党”利益的最突出的代表人物。尽管“上海帮”的数量在省级领导人中非常小，有几个重要的职位仍被他们占据，如上海市市长韩正和重庆市市长黄奇帆。太子党成员和上海帮成员通常在经济管理、外贸和金融方面有更丰富的管理经验。俞正声和薄熙来之前曾分别担任建设部部长和商务部部长。韩正和黄奇帆是省级最高领导人中少有的金融专家。他们这种难得的专业资格和领导才能将可使他们成为未来的领导层更替中不可或缺的重要人选。<br />
