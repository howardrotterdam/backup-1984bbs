18届党的代表大会预期领导层大规模更替<br />
尽管中国政治体系的威权本质和一党专政，中共的领导层在过去三十年中却明显地具有较高的更替频率。事实相当明显，1982年12届中央委员会中新成员占60%，1987年13届这一比率为68%，1992年的14届为57%，1997年的15届为63%，2002年的16届为61%，2007年的17届为63%。平均更替率为62%。12 根据现有中央委员的年龄分布和之前党的代表大会的更替率，我们可以粗略估计18届中央委员中的60%将会是新面孔。<br />
在中国主流精英圈中，领导人的年龄是其未来政治前景最重要的指标。按照中共的规则和标准，某一级别的领导人年龄不能超过一个设定的限制。例如，所有省级负责人在到65岁时都被要求让位，只有那些年龄低于63岁的才会在一开始成为候任人选。在2007年的17届党代会上，所有1940年之前出生的领导人，包括当时政治强人的国家副主席曾庆红（1939年生），都不允许在新的中央委员会中任职。这些退休年龄限制不仅确保在领导人退休和推荐上的一致和公平，而且使中国政治精英更换速度相当迅速。对于外界的观察家们，在即将到来的党代会上谁将会进入或者退出最高领导层――读懂北京的逻辑――已经不像以前那样具有挑战性。 <br />
表2（见下页）详细列出当前25个政治局成员在2012年的18大之后可能的仕途前景。考虑到上届党代会上允许的最早出生年限是1940年，对于即将到来的党代会当局将会选择1945年的年限。如果是这样，至少14个成员（占总数的56%）将会退休。在9个政治局常委中只有两人即国家副主席习近平和常务副总理李克强出生于1945年之后，因此将保持他们的席位。习和李被广泛认为是“双头继承模式”。其余7人（占总数的78%）都可能将席位留给更年轻的现任政治局委员，最有可能的是李源潮、王岐山、刘云山和张德江。中组部部长李源潮和副总理王岐山现在分别负责党的人事和国家金融发展。负责两个关键领域已经使李和王的政治分量相当重要，他们预期将在今后几年扮演更为重要的角色。中宣部部长刘云山和副总理张德江已经担任两届政治局委员，很有机会提升为常委。<br />
