职业道路、领导经历和政治派别<br />
绝大多数现任省级负责人属于所谓失落的一代，这一代人在文革（1966-1976）中度过他们的成长时期，失去了获得初中和高中教育的机会。相反，很多人成为“下乡青年”，青少年时代就被从城市派往通常很遥远的乡村，在多年甚至整整10年里从事着体力劳动。23个省级负责人（37%）――包括四川省委书记刘奇葆、辽宁省委书记王珉、广西区委书记郭声琨、海南省省长罗保铭（1952年生）以及上海市市长韩正（1954年生）――都遭受这一命运。例如，新任命的河南省委书记卢展工1952年生于浙江省的慈溪市，但早年在偏远的黑龙江省度过了13年的光阴，其中包括4年的农场劳动。<br />
 <br />
多个省级负责人来自农民家庭。例如，天津市市长黄兴国1954年出生于浙江省象山的农村地区，他最初的政治晋升是在当地的一个人民公社。与此类似，内蒙古自治区主席巴特尔是17岁时在内蒙古的一个农场作为放牧人开始其职业生涯。很多省级负责人即使没有下乡青年经历，也在生涯早期有过底层的工作经历。山西省委书记张宝顺（1950年生）曾是码头工人；山西省省长王君（1952年生）曾是煤矿工人；广西自治区主席马飚曾是钢铁厂工人；重庆市市长黄奇帆曾是化工厂工人；西藏自治区党委书记张庆黎（1951年生）曾是化肥厂工人；广东省委书记汪洋曾是食品加工厂工人。多个省级负责人开始职业经历时是士兵，包括青海省委书记强卫（1953年生）、山东省委书记姜异康、江苏省省长罗志军（1951年生）、宁夏自治区委书记陈建国和西藏自治区主席白玛赤林。<br />
<br />
文革所造成的特殊生活经历对每一个领导人都有着持久的影响，并且整体上对第五代有一种独特的集体冲击。这一代的成员与第三代和第四代领导人不同，文革开始之时后者已经完成了他们的大学教育。还有一点应当提到，基于青春期的经验，第五代领导人整体上与未来的第六代领导人有深刻的差异。后一个群体是在一个物质上丰富的时期直接从中学升入大学，很少需要面对前一代人遇到的苦难和挫折。也有迹象表明第五代的艰苦和贫贱的经历使其成员培养出有价值的个性，如坚忍、适应性强和谦虚。正如河南省委书记卢展工所说：“我只是成千上万个下乡青年中的一员，我和我的下乡青年伙伴之间没有多少区别。唯一的区别是我足够幸运，抓住了给予我的机会。”23&nbsp;&nbsp;<br />
 <br />
最近中国官方媒体发布了一个中国31个现任省长和市长的研究，显示这些领导人中有77%开始职业生涯的时候是下乡青年、工厂工人或士兵――几乎没有白领职员。24 他们参加中国共产党的平均年龄是23岁，开始担任政府官员工作时平均29岁，升到省长职位时平均54岁，现在平均年龄57岁。这个报告强调，平均而言，这些领导人在担任省长之前都有25年的领导经验。<br />
