有一点很重要，这些省级负责人获得的硕士学位或者博士学位大部分是在职的，很多人事实上是在中央党校获得的高级学位。近年来全国上下都强烈批评这种“假学历”。近来中国官方媒体呼吁官员们做到“学历清清白白”。19 中国媒体也曾报道说某些高级官员甚至是从一些被批评为“国外冒牌大学”获得他们的“高级学位”。据说，这些“高级学位”资格，比如EMBA，是不被美国教育部承认的。20 近期被控腐败而遭清洗的两个高级官员，深圳市前市长许宗衡和文化部前副部长于幼军都是从此类途径获得他们的学位。<br />
