在政治局16个非常委成员中，除了4人可能如上所述晋升为常委，至少7人将因为年龄限制而下台。其他官僚机构如中共中央各部门、国务院和军队中的重要领导人也将获得部分席位。例如，现任中共中央办公厅主任和书记处书记令计划极有可能在下届政治局中获得一个席位。作为胡锦涛的亲信，令甚至有可能提升为政治局常委，与2002年时发生在江泽民的亲信曾庆红身上的情况一样。13 然而，现任的省级负责人们可能占据这14个公开席位中的绝大多数，包括3个常委会席位（如果政治局常委会仍然保留9人成员的话）。<br />
