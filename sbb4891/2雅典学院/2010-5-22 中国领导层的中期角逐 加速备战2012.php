													<h2>中国领导层的中期角逐 加速备战2012</h2>
						
						
																											<div id="postmessage_631002" class="t_msgfont">作者：李成<br />
单位：约翰&#8226;桑顿中国研究中心 布鲁金斯研究所<br />
发表时间：2010年3月<br />
译者：Andy Cheng<br />
校对：xiaolin Wong<br />
<br />
中国将在2012年第18届中国共产党全国大会上更换主要领导人。现有最高领导人中，国家主席胡锦涛、总理温家宝、全国人大委员长吴邦国预期都将退休。大量新的面孔将进入政治局及其常务委员会。谁是这些高级领导群体最有希望的候选人？新晋者的主要性格特征以及晋升的标准是什么？我们能否敏锐地预测可能的领导集体的阵容以及权力的派系分布？新一代领导人会在多大程度上改变中国的政治运作方式？通过研究中国31个省级行政区的62个省级负责人――党委书记和省长们――本文试图阐释上述及其他相关的问题。毫无疑问，今天的省级负责人中将会产生明日的国家决策者。我们可以合理预测，在这个十年的大部分时间以及今后，这些领导人中的一部分将会统治这个世界上人口最多的国家。<br />
与美国一样，2012年对于中国的政治家而言是一个重要的年份。自从1977年以来，中国共产党每隔5年定期举行全国代表大会。党的大会一直是中国最高领导层更替的重大时刻。预期将于2012年秋季举行的中共第18次全国代表大会也不例外。国家最高决策群体政治局常委会的9名成员中有多达7人预期将会退休。在25人的政治局中，至少14名领导人将空出他们的位置以让位给更年轻的候选者。结果，2012年之后的新晋者将成为对国家的政治和意识形态事务、经济和金融管理、外交政策、军事运作等方面负责的主要人物。1<br />
尽管北京领导人的大换班感觉仍很遥远，中国的政治家们却已经开始行动。当然，中国没有像美国那样的中期选举。对于中华人民共和国的政治主角们，美国中期选举的实践仍是一个外国新事物，但正如“中期”在美国代表将要来临的总统选举之前的一个重要中间节点，中国的政治时钟也遵循着明显相似的节奏。在主要竞争者以及他们所在的派系之间，对于权力的激烈角逐至少在“领导权更替的重要年度”之前两年就已经升温。<br />
<br />
这个特殊的时间段主要是中共最近制定的规则和标准的作用，要求高级官员（县长及以上）在获提升之前至少需要任职两年。2想进入新的中央委员会和新政治局的候选者们在当前及未来的数月中必须精明的卡位，在中央或省级领导层占据党和政府内的晋升阶梯。<br />
因此，分析这些显要职位的在职者――尤其是在最近的洗牌中新获任命的――对于中国观察家们极有价值。这种分析可以帮助我们达到四个重要目标：1）揭示下届政治局可能的领导群体，包括常委会成员；2）展示新领导人的政治和职业个性，包括年轻一代中冉冉升起的新星；3）对18大之后中国领导层内部的派系权力平衡进行评估；4）预测新领导层之下的国家政治轨迹和政策导向。本文是关于中国政治家们在迈向2012年领导层更替过程中对权力的角逐专题系列的第一篇。本专题将考察若干最重要的官僚机构，包括中共中央各部门、国务院各部委、主要国有企业（SOEs）以及军队。第一篇文章聚焦于现有的62个省级主要负责人――省委书记和省长（或市长）――他们分属于31个省级行政区。3文章对62个省级领导人的简历背景、教育资质、职业生涯以及政治网络或者说派系身份提供了一个综合性的经验分析。除了部分与他们的派系渊源和家庭背景有关的信息之外――这些信息是基于作者的访谈和非官方的中国媒体――，所有的数据都来源于官方新华社的网站。4<br />
这62个省级负责人或许是观察中国中期选举的最重要群体，因为中国的省级领导层既是国家领导人的训练场，又是不同政治力量的战场。省级层面的权力派系分配与在国家层面一样分明，因此值得我们注意。分清楚省级负责人的晋升途径，是通过共青团（胡锦涛的权力基地）、“太子党”（指那些有高干家庭背景的领导人），还是“上海帮”成员（曾是江泽民执掌上海时的手下的领导人们），是预测今后数年内中国政治版图的最佳途径之一。<br />
<br />
<br />
省级领导层：中国最高领导层最关键的阶梯<br />
<br />
中国的省级负责人在当前国家治理中承担着巨大的政治比重，这有三个原因。首先，这些领导人治下的省或市都是大型社会经济体。经常听到的说法是中国的一个省就相当于欧洲的一个国家。实际上，中国的省份在人口上超过欧洲绝大多数国家。例如，中国人口最多的5个省：河南、山东、四川、广东和江苏，人口超过西欧人口最多的国家：德国、英国、法国、意大利和西班牙。5 河南省现在拥有9970万人，预计在2010年7月超过1亿。6除了中国，世界上只有10个其他国家的人口超过1亿。7这些省份的经济意义也很重要。例如，广东省总的GDP已经超过“东亚四小龙”中的三个：新加坡、台湾和香港。广东省省长最近宣布该省的GDP将会在10年内超过另一个“小龙”，韩国。8中国的省级负责人们就像欧洲国家和东亚四小龙的领导人一样，持续关注区域经济发展，并且要应对艰巨的挑战，如失业、分配的公平、社会稳定、医疗卫生以及辖区内人们的福利需求。 <br />
<br />
其次，中国的省级领导高层本身就是一股政治力量，尤其是当省级政府拥有更多自治权限来推进他们的地区利益之时。尽管通常处在幕后，他们仍会时常参与政治网络、政策游说、在他们自己之间和中央权力之间建立联盟。过去15年间，中共最重要的两次政治清洗都发生在两位兼任省级负责人的政治局委员身上，并不是巧合。第一位是1995年时任北京市委书记的陈希同，第二位是2006年时任上海市委书记的陈良宇。中华人民共和国的最近两位国家主席，江泽民和胡锦涛，通过在省级层面上清除掉这两位强大的政治对手，以巩固他们的权力和中央政府的权威。最近，省和地方的“驻京办”数量增长迅速，它们是中国以地区为基础的游说团体。2010年1月，中央政府发布新规，大量减少获准的代表地方利益的驻京办数量，并且要求对剩余的省/市级别的游说群体进行财务审计。9 <br />
<br />
第三，也是最重要的一点，在邓之后的中国，省级负责人的职位一直是通向国家领导层位置最关键的阶梯。这一时期绝大多数中国最高级领导人，第三代的江泽民和朱F基、第四代的胡锦涛和吴邦国、第五代的习近平和李克强，在进入北京就任国家最为显要的领导职位之前，都曾担任省或市的党委书记。在现任的九大政治局常委中，除总理温家宝之外，之前全都有省级负责人的经历。高级领导层中正在升起的6颗新星曾担任或现任省级负责人，他们都是属于政治局成员中的第五代。 10图1表明，政治局中有省级负责人经历的成员显著增长，1992年为50％，1997年为59％，2002年为67％，2007则达到76％。对于文职领导人，省级负责人的经历几乎已经是入选政治局的先决条件。<br />
