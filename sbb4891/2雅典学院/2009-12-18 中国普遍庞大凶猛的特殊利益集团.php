美国《世界日报》12月1日载文《中国“特殊利益集团”困扰加剧》，摘要如下： <br />
<br />
中共今年最后一个重要会议中央经济工作会议召开，来年经济和社会发展大计敲定。这次会议以政策不变、全面维稳为基调，既反映出内外形势的复杂，又折射着 中国经济和社会发展的矛盾重重，令中共决策层举棋难定。而在中国发展中面临着的，除官民矛盾、民族矛盾、政教矛盾之外，目前一个较大的困扰，是特殊利益集 团问题。 <br />
<br />
今年以来，与特殊利益集团相关联的争论、事件，可谓层出不穷，遍地开花，从地方政府大开涨戒，从水到电等公用事业大加 价，到垄断集团玩弄油荒、气荒之术，逼宫涨价；再到巨型国企和地方政府联手炒地，“地王”频现，最后是竞争性行业中，出现了“国进民退”潮。这一切，都被 认为是特殊利益集团的翻云覆雨。&nbsp;&nbsp;<br />
<br />
但真正值得关注的，还是围绕着中央经济工作会议，围着明年经济和社会发展的政策，展开的争论，特 殊利益集团及其代言人，再次全面施展其能量，试图影响政策的制定。诸如中国经济是否出现“拐点”，是否面临通胀，应否纠正“国进民退”，该不该拿出退市安 排，结构如何调整，如何增收入扩内需等尖锐的议题，都没有拿出答案。 <br />
<br />
因为特殊利益集团的再度坐大，利用中共领导人有保持政策连续性的偏好，及领导人但求稳定第一，成为特殊利益集团防变的护盾，中国经济和社会发展，再现隐患。 <br />
<br />
举例而言，特殊利益集团为防大投资带来的利益受到紧缩政策的冲击，危言耸听，推动积极的财政政策和宽松的货币政策不变，这有可能令中国经济，面临泡沫、虚热、通胀等一系列问题。 <br />
<br />
中国所谓特殊利益集团，直白点说，就是以特殊的权力或优势，掠夺国家和民众利益的集团，他们获得的“特殊利益”，不仅是利润的最大化，还从资源的占有、权力的操纵、舆论的玩弄，一直到司法的护航，无所不有。 <br />
<br />
中国的特殊利益集团，还真有其特殊性，一是其普遍性，各类组织各类行业，从上到下，有特殊利益的地方，就形成大大小小的集团；二是其最庞大性，由于中共 要保社会主义公有制，因而要维持一批国有垄断企业，集中在金融、电力、交通、电信、能源等垄断行业，以占有巨额国有资产及公共资源，形成庞大的垄断利益集 团；三是其凶猛性，特殊利益集团不仅有不同组织与政府部门的勾搭，还有政府部门因“利益部门化”，由掌握了公共权力的政府职能部门，蜕化成与国家和人民利 益背道而驰的自利集团。 <br />
<br />
现在的问题在于，中国的特殊利益集团除了掠夺利益外，已将手伸向更广的领域，有人认为渐渐形成类黑社会组 织。以经济领域而论，特殊利益集团不仅已有经济学者为其发声，还已成功地令政府主管部门成为其代言人。中共领导层制定一揽子救市计划，今年近十兆元人民币 大投资，九成落到国有大企业手里，就是明证。</div>

							
							
							
							
