中国固然问题很多，但是请记住，你不是生活在前清，也不是生活在解放区，也不是生活在黄金十年，你就是生活在当下，你没有办法穿越到任何一个你想去的时代。<br />
<br />
时间上不可能，地理上你也许考虑过移民，但是你也知道，如果移民了，你就不会对这个国家保有激情了。你会很快的沉入到那个社会的问题中，然后发现每个社会都有自己的问题，只不过有程度和发展阶段的区别。而且你会发现自己如同外来人，自己关心的东西别人不关心，自己充满激情的东西，别人无所谓。当你想摇旗呐喊的时候，却发现别人不当回事。你还需要和那些被认为是宗教的东西挣扎，然后你发现很快陷入许多海外华人的类似心路历程，那就是，你也开始不能容忍别人批评这个国家，而愿意为这个政府辩护，所谓只有海外华人才是爱国的，就是这个意思。因为你需要更多的认同感。<br />
<br />
问题是，当你兴高采烈的赞赏外国的民主制度的时候，甚至蹑手蹑脚想在那样的制度下做些什么的时候，你始终没有办法绕过去的一个问题就是，为什么你那么有激情，却没有为自己的国家做些什么？<br />
<br />
所以除了感慨这个时代，你还得看看能不能为这个自己的时代做些什么。<br />
<br />
比方说，你抱怨中国司法不公，你有没有亲力亲为的申请做人民陪审员？<br />
<br />
比方说，你有没有养成一天打上5个市长热线电话的好习惯？哪怕没有人接听，或者虽然有人接听，但是没有人处理你提的问题？<br />
<br />
比方说，你有没有向报社投笔，把当地的氛围，搞得像广州那样的相对透明？广州的报纸可以大胆的批评亚运会对当地居民的骚扰。<br />
