													<h2>每年见维园点点烛光　都感到震撼　柴玲：香港是中国的良心</h2>
						
						
																											<div id="postmessage_654070" class="t_msgfont">AppleDaily苹果日报网上版－－2010-06-02<br />
--------------------------------------------------------------------------------<br />
每年见维园点点烛光　都感到震撼<br />
柴玲：香港是中国的良心<br />
<br />
<img src="http://thumbsnap.com/i/CB4Z7CuE.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
柴玲与丈夫和幼女的合照，柴玲说，现在为中国被迫堕胎的妇女发声，与她是母亲有莫大关系。　柴玲提供图片<br />
<br />
【六四二十一周年专版】21年前的六月四日，23岁的柴玲从北京天安门广场撤退，逃过死亡，却陷入黑暗，直至去年，她重新上路，成立基金支持中国民主运动。柴玲说，香港是中国唯一可公开悼念六四的地方，是「中国的良心」，每年她在美国看到香港维园的烛光晃动，都「非常震撼，非常感激」。　记者：陈沛敏<br />
<br />
还有两天，又是六四。流亡美国的八九学运领袖柴玲昨日接受本报电话访问，对香港发生民主女神像被没收的事件，她说那是当局不守「一国两制」承诺的表现。她去年宣布成立100万美元的基金，支持中国人权和民主运动，至今捐出20多万元，除帮助六四受害者、内地孤儿和被拐卖儿童，还有香港支联会的六四话剧。<br />
<br />
逃离中国后曾说不想活<br />
<br />
她想跟香港人说：「香港民主很重要，不要气馁。中国会自由的，要有信心。」去年六四20周年，20万人在维园高举烛光，她震撼又感激，「这是非常重要的，大家必须出来。」她说，香港是中国土地上唯一可公开纪念六四的地方，意义非常重大，因为香港代表着中国的良心。今年六四，她会在华盛顿一家著名教堂举行祈祷会，为六四死难者和中国民主祈祷。<br />
<br />
过去20年，柴玲很低调，一直回避传媒。她逃离中国后，经历了离婚、流产，在巴黎一次海外民运人士聚会，甚至说过「不想活」，「一个人走不下去了」的话，后来因为《天安门》纪录片等报道，外界批评她「要别人流血，自己逃生」之声不绝。<br />
<br />
「过去20年里，我一直有一种很深的挫败感，天安门运动被镇压下去了，只看到黑暗。」柴玲说，民运被镇压，加上多年来受到攻击诽谤，她把心灵关闭起来，「现在我却看到光明的到来。」去年六四20周年，因为基督教信仰，她复出民运，「成为基督徒后，感觉到上帝的公正和恩典，也感觉到上帝对我的呼召。」也同时因为遇上六四镇压时为救同学、被坦克辗断双腿的方政。<br />
<br />
方政对她说，「越来越多人从中国的经济发展中受益，也淡忘了当年的牺牲。天安门事件的受害者被遗忘了，他们每天都在贫穷和疾病中挣扎。」柴玲猛然惊醒，宣布成立基金，但之后却遭到中国政府的隔空施压，「中国政府代理要求我的家庭成员直接传达给我一个消息，那就是我捐款不会给我以及我的家庭带来任何好处。尽管生活在美国这片自由的土地上，我仍然感到乌云时刻笼罩着我。」<br />
<br />
为被迫堕胎的母亲发声<br />
<br />
她感到受监视跟踪，计算机被入侵，详情她不愿多谈。但她说，同为八九学运领袖、现为牧师的张伯笠今年复活节替她发出信主见证文章后，翌晨开车到教会上班时突然遭到一部车从后撞击，造成轻微脑震荡。跟着，她家一棵百年大树突然倒下，幸无伤及家人，她认为那是神迹。但更「神奇」的是，过了两天，曾向她传话的那个中国政府代理说，为她的信主见证「感动」。<br />
<br />
去国21年，一直无法回家，但柴玲说她从未忘记中国人民。除成立基金，她还发起「女孩也要」运动，为国内计划生育下被迫堕胎的母亲和孩子发声。现已是三子之母的柴玲说，因为是母亲，她明白作为母亲愿意为孩子牺牲一切甚至生命的心情，因此她早前陪同一位在内地被强制堕胎的母亲到美国国会作证时，听到成形的胎儿被强行剪碎的场面，泪流满面。<br />
<br />
北京办奥运，上海办世博，强国崛起，柴玲说：「一个暴虐的强国，是很可怕的一件事，但一个和平的强国，可以是对人类很大的造福。」</div>

							
							
							
							
