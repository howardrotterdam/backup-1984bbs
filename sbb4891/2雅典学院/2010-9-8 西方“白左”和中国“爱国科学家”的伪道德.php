													<h2>西方“白左”和中国“爱国科学家”的伪道德</h2>
						
						
																											<div id="postmessage_818558" class="t_msgfont">五毛们众多的口头禅之一就是“太平洋没加盖儿，你倒是游到美国去啊？”要么就是“你倒是用脚投票啊！”还真不用您提醒！试想要不是美国国土安全部拦着啊，天朝奴隶早就一溜烟儿都跑过去了，奴隶主肯定也得跟进。而现实是奴隶主先行一步，纷纷拿到绿卡，留奴隶们在那儿干靠。有个笑话，说墨西哥人最恨的就是美国人，恨他们抢走了加州和德克萨斯，而没把整个墨西哥都抢去，害的老墨偷渡跟TM翻柏林墙似的。我心想啊，美国人就偷着乐去吧，幸亏丫没跟天朝接壤，不然挤死丫的。<br />
<br />
&nbsp; &nbsp; 不过就这也阻挡不了天朝的知识精英呼呼的往美国跑。古人云“来而不往非礼也”，天朝不但在物流上被美帝非礼，一船船的货真价实的商品运到美国去，换来的却是堆积如山的冥币（见《人民币？人冥币！》），人流上也被美帝非礼，一飞机一飞机货真价实的的知识分子运到美国去，换回来的是零星的“爱国科学家”以及更为罕见的“白左”，前者以钱学森、钱伟长、萧光琰为代表，后者以阳早、寒春夫妇为代表。这又印证了两句古话：1 得道多助，失道寡助。2 林子大了什么鸟都有。<br />
<br />
&nbsp; &nbsp; 由自由世界回到天朝的“爱国科学家”有三种：1不得不回国，但是脑子拎得清的投机者，比如钱学森，这种人是人精，混的比较好，但是人数非常少。2一腔热情主动回国且生命力顽强的，比如钱伟长，这种人不通世故，以致被斧头帮迫害，最后学术上无所成不说，还患上了斯德哥尔摩综合症。3一腔热情主动回国且生命力脆弱的，比如萧光琰，这种人不通世故，以致被斧头帮迫害，直接自杀！<br />
<br />
&nbsp; &nbsp; 关于钱学森先生的智商和学术水准，恐怕这个星球上鲜有人能够不服，但我要说的是其厚黑学功力更是无人能够企及，他善于把自己包装成一个“爱国者”，他曾说到：“我在美国前三四年是学习，后十几年是工作，所有这一切都在做准备，为了回到祖国后能为人民做点事。因为我是中国人。”“我的事业在中国，我的成在中国，我的归宿在中国。”which is totally untrue! 事实上，钱的回国并不是他自己的选择，相反，他在1949年申请加入美国国籍，但是不久就遇到麦卡锡主义盛行，钱被迫害并吊销了涉密权限，并且被美国政府下令驱逐出境，这一切都白纸黑字的写在Caltech的官方网站上，好信的可以自己看<a href="http://today.caltech.edu/today/story-display.tcl?story_id=39604" target="_blank">http://today.caltech.edu/today/story-display.tcl?story_id=39604</a> ”麦卡锡主义最终被证明是错误的，对钱的指控也是莫须有，美国也因此失去了一位优秀的科学家！美国是自作孽不可活，被土共捡了个漏！不过话说回来，美国连迫害都迫害的这么没水准，最终还是放了人，要放苏联早秘密枪决了，然后放话“这个人不存在！”可以想象，如果麦卡锡主义没有盛行，钱学森作为第一批接触德国飞弹技术的美帝一流科学家，日后的成就未必比冯・布劳恩祖师爷小。很难想象，一个人，一个以科技进步为己任的科学家，面对这样的自我实现名留青史的机会会毅然放弃，而他的所作所为完全符合我的猜想而与他自己声称的相悖。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;钱在回国后的种种行为完全违背了一个科学家所应该坚持的原则――对真理的追求和对事实的尊重，亩产十万斤的大卫星就是他放的，人民日报有据可考。头一次听说这事儿还是在北航的法律基础课上，老师跟我们讲的，当时真的很难想象这就是铜像被摆放在北航图书馆前每天被无数学子崇拜的人干出的事情。钱之所以能在体制内混得那么好，阿谀逢迎拍腊肉马屁功不可没，有了这样一个大科学家的“理论支持”，腊肉进行“逆向工业革命”恐怕更是信心满满了。数千万人饿死，钱老功不可没。也许钱老只是因为比别人聪明而看破了时局，他的所作所为只是迫不得已。“你不为虎作伥助纣为虐你就得死！”而且，钱老回国本来就是迫不得已，又不是自愿，这完全就是被美国的麦卡锡主义置于万劫不复的境地，不赖钱老。到这里我们只能说，钱学森只是智商超高的普通人罢了，并不是什么道德上的巨人，所谓爱国都是谎话，坚持真理上又比不过布鲁诺哥白尼，最终不得不沦为体制的帮凶和道德上的苟活者。说钱智商高可以，说他是牛人，没人敢不承认。说钱学森如何高尚的可以直接在自己脑门上贴个标签，上书“傻逼”二字。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;都说了钱学森了，就不能不提俺们的冯・布劳恩祖师爷，此爷从小到大就一个梦想：把人送到月亮上去。谁给钱他就给谁干，有奶便是娘。这姓冯的一看名儿就是个德国人，当年必然是给元首干，元首让他研究飞弹，这位爷可倒好，一边给元首造飞弹一边把元首的钱贪污掉自己拿去搞登月火箭研究，一边薅羊毛，一边纺线，一边织毛衣，一边再薅羊毛…………。待到纳粹垮台，这羊也差不多成葛优了。冯爷带着火箭资料以及手下一帮小崔巴儿主动跑到美军那里，说“哎呀你们可来啦！V2你知道不？爷设计的！想要火箭的话快把爷抓到美国去！”就这样，冯爷和他的德国小崔巴儿们到了美国，成了美国航天事业的生力军，以至于到了70年代美国的航天控制大厅里面回荡的还都是&quot;Zehn Neun Acht Sieben Sechs……Eins Zero , Starten! Ja, das ist gut !&quot; 搞的老美特不自在！冯爷的一生是幸运的，他拥有一个伟大的梦想，并且通过自己超凡的智慧和不懈的努力实现了梦想，眼看着自己一手把人送到月亮上去，用纳粹的羊毛给美国和全人类织了一件大毛衣，最终得以含笑九泉。而他曾经为元首服务过这一污点也因他最终弃暗投明和他对全人类的自由和幸福的巨大贡献而被逐渐淡化。冯爷和钱老不同之处就在于，冯爷主动弃暗投明，钱老被迫弃明投暗。现在看都是名留青史，因为纳粹倒了，斧头帮还没倒，长远看，钱老还得被青史再驱逐一次！<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;钱学森也算是不幸中的万幸了，起码活下来了，而且活得还不错。钱伟长就没那么幸运了，57年反右被打成右派，到1976年这20年一篇paper都没发，作为一个科学家，这正是事业发展的黄金时期，可惜钱不得不被下放劳动，远离自己的科研领域，就连得到个给工人夜校讲课的机会也要对裆感激涕零，这还不是最坏的，华罗庚回国时说“良园虽好，却非久居之地”，十年之后他却对妻子说“我想自杀”，震惊海内外。而华回国后在学术上也毫无建树，不得不放弃数论，去搞“爱国主义教育”。我爸的一个大学同学的高中同学是唐敖庆老先生的儿子，据说唐老在文革期间被迫去打扫厕所，时不时的还要忍受红卫兵的打骂。我估计天朝如果报道这事儿就得这么说了“吉林大学一个扫厕所的都是享誉世界的化学家和杰出的教育家，哥伦比亚大学化学博士！”就像卡斯特罗爷爷骄傲的宣称“我们古巴的妓女都有大学学位！”那样。这还不是最惨的，芝加哥大学毕业的萧光琰博士，从美孚公司带了大量资料回国，在文革中不堪忍受侮辱和酷刑，干脆自杀，最后还被扣了个反革命分子畏罪自杀的屎盆子。他临死还不停的呓语“裆的政策不是这样的……”助纣为虐最后被一脚踢开的时候还念叨“纣王不是这样的，这不是我们的纣王”这不是高尚，这是愚蠢！历史上，愚蠢所带来的苦难要远远的多于“不高尚”！ 同样，苏联帝国被祸害惨的也不少，科罗廖夫朗道啥的。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;如果说科学家要有学德的话，回天朝的科学家不是缺德就是脑袋拎不清，显然科技工作者要以以探求真理和增进人类之自由和幸福为己任，斧头帮的御用科学家，比如两蛋元勋，不但没有增进任何人的幸福，反而让流氓会了武术，斧头帮有了蛋蛋，对内奴役屁民对外挥舞大棒耍横更是肆无忌惮，你怎么看金二胖的蛋蛋科学家，全世界就怎么看天朝的所谓两蛋元勋！而且斧头帮科技工作者跟纳粹科技工作者还有一个不同，纳粹科研人员都是原创，而且原创的非常有水准，从喷气式飞机到冯爷的阿波罗，航空航天的几乎所有概念和prototype都出自纳粹科研人员，纳粹倒台之后，这些智慧的结晶很快扩散到全世界，而且改变了全世界！你从帝都飞魔都都要感谢纳粹雇佣的那帮牛人。而斧头帮科学家只会山寨自由世界的东西，鲜有自己的原创，即便斧头帮倒台，这些东西对全人类价值也不大。科学无国界，科学家也不应该有国界！所谓“民族大义”就是胡扯，跟“民主专政”一样荒谬，民族大义只在你的民族永远正确，永远站在道德制高点，永远站在人类的文明和进步一方的时候才能成立！事实上根本没有这样的民族！只有纳粹德国和苏联这样的邪恶帝国才会搞出什么“德意志的科学”和“犹太人的科学”；什么“社会主义的科学”和“资本主义的科学”。钱永健先生可能保持了钱家拎得清的遗传优势，面对爱国贼满心欢喜的提问，他说“我不是中国科学家。我在美国长大，并一直在这里生活……”<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;所以，如果你是一个科技工作者，在面临人生选择的时候，你是不是应该想一想：我是能增进全人类的幸福还是只增进某个小集团的幸福而危害全人类？尤其是那些北航和哈工大的学子，你们是应该为万恶的一小撮制造杀人机器还是应该用自己的专业知识尽快解救自己在苦难中的同胞？能力越大，责任越大，科技工作者是掌握着未来的人，你们的选择不但需要激情的支撑，更需要理智的判断！<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;“林子大了什么鸟都有！”那些从自由世界弃明投暗的不但有华人，也有白人，曰“白左”。还是来而不往非礼也，不过那些从苏联和天朝逃到自由世界的都是些大牛之人，比如被誉为俄罗斯的良心的索尔仁尼琴，比如所有十位华裔诺奖得主，比如大喇嘛，比如高行健。喜欢看美剧的也许还记得，《逼哥帮》里有一集，谢尔顿都答不上的问题居然被一个看大门的给答上了，其实人家是列宁格勒技术大学的博士。也许有些夸张，但是苏东科学家确实有不少跑到美国的。反观西方“白左”，来这边的都是些小喽，白左的“扛把子”才不傻呢，比如美国乔姆斯基爷爷，人家天天在家门口骂美国这不好哪不好，但是照样从来没有动过逃到苏联和天朝的想法，他自己的解释是“美国仍然是世界上最伟大的国家。”他也就只停留在为了骂而骂，还不如韩寒呢，他才是根本提不出什么建设性意见，美国两个多世纪积累起来的精密系统至少目前是运行最良好的，你以为人家不改么？美国光宪法修正案提案就有一万多条。乔爷爷在专业领域语言学之外除了向全世界兜售激情和愤怒也不会干别的。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;而来到中国的阳早寒春夫妇就名不见经传了。这俩美国人将毕生精力都用在了批评美国和给中国人养奶牛、让中国人喝上好牛奶的伟大事业上，就在今年，寒春女士死了，对于她的死，我想引用一句名言“有些人活着，他却死了，有些人死了，他死得好！”其实寒春女士跟乔治・华盛顿先生以及冯爷有一个共同点，他们都是理想主义者，理想也都不小，华盛顿要建立当时根本不存在民主共和国，把权力关进笼子，冯爷要把人搞到月亮上去，寒春女士要让中国人喝上好牛奶。他们的不同之处在于，前两位成功了，后一位失败了！咳咳，there we have ：1美国、2阿波罗计划、3三鹿+圣元！ 这就是务实的理想主义者和傻逼理想主义者的区别。寒春女士死前还奉腊肉思想为圭臬，并坚决认为“无产阶级文化大革命就是好来，就是好，就是好啊，就是好！”事实上寒春女士在一般人看来是个道德高尚的人，她不但生活简朴，而且时常自掏腰包接济穷苦人，她只不过对大跃进几千万人的惨死和文革数百万人惨死熟视无睹罢了。而作为一个白人，一个美国人，一个国际左派，她的立场比千千万万个普通中国人都要更有代表性更有影响力，而她选择了为了自己偏执的理想和对纯洁道德的清教徒般的追求而选择性无视了这世上最大的苦难！在我看来，对大恶的容忍和沉默所造成的杀伤远远大于一些小善所带来的慰藉；缺乏理智引导的崇高理想所带来的破坏远远大于这些理想本身所带来的美好！<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 一旦对崇高理想的追求被异化为一种狂热的宗教，理想也就失去了意义，理想的追随者也就变成了魔鬼，实现理想的过程就会变成无尽的杀戮和永不停歇的毁灭。多年以前我便认为，理智不但是一种素质，更是一种美德，是一种凌驾于一切美德之上的、美德中的宪法！寒春女士接济过的穷苦人，以及他们的后代，想必现在仍然是穷苦人罢！</div>

							
							
							
							
