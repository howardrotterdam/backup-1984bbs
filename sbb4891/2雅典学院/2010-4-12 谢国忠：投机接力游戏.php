													<h2>谢国忠：投机接力游戏</h2>
						
						
																											<div id="postmessage_560381" class="t_msgfont">　　中国经济让投机者获益，而不是努力工作的人们。这也是为什么越来越多的公司和个人进入房地产市场<br />
　　未来十年将充满挑战。由于低成本和不断扩大的外需，中国经济增长引擎一直是出口和外资。未来十年，中国不再是低成本的生产者，不大可能继续扩大其全球市场份额。另外，外需也不会像过去那样快速增长。即使政策得当，经济增速也只会有过去的三分之二。<br />
　　目前，中国经济最大的危险是为保持高增长而将房地产投资最大化，而房地产并非生产型资产。房地产行业通过牺牲长期经济增长来支持短期增长，其资本平均生产率随着时间推移而下降。地方政府的政绩主要通过GDP和财政收入体现。房地产开发可以帮助地方政府迅速提高这两项指标。由于价格和面积均快速增长，房地产在中国资本分配的份额中大幅提高。这是由于政治因素而导致的泡沫，而且这个泡沫已经很大。除非通过改革地方政府的评估机制来扭转这一趋势，中国未来两年的资产泡沫将越吹越大，而目前的状况已相当危险。泡沫可能在2012年破裂，届时中国社会和政治稳定都将受到威胁。<br />
　　在中国，过去五年中，生产主要要素（劳动力、原材料、土地、环境控制和税收）的成本都在增加。工人工资以人民币来算增加了大约1倍。原材料价格也增加了1倍。亚洲金融危机前，中国工资水平是东南亚国家的一半，现在则是那些国家的2倍。孟加拉工人的工资甚至更低。在全球贸易中，中国很可能会输给这些更低成本的竞争者。<br />
　　中国过去十年成功三要素是低起点、运气和改革，但现在，其中的两个已不复存在。现在中国需要提高效率来促增长。但是，目前的形势似乎向相反方向发展。成本增加和需求低迷使制造业的利润减少。资本投入也在放慢，体现在进口设备的速度放慢。多数地方政府似乎把房地产行业视为经济增长的救世主。将过剩资本投入房地产，不仅会导致未来经济增长放慢，也会降低平均资本效率。同时，由于房地产开支预期增加，日常消费受到遏制；以价格虚高的土地为抵押的贷款更是威胁金融稳定。<br />
　　东亚地区经济体，如日本、韩国和中国台湾，都无法摆脱对出口的依赖。阶段性的消费上升通常是由于资产价格上升，难以长期维持。一旦由于成本增加而失去出口市场，它们都会陷入经济停滞的困境。因为高增长时期的资产泡沫使中产阶级成为“房奴”，无法进行其他消费。中国或许正在同一道路上：一年又一年，人们在谈论着增加消费，消费在国内生产总值中的份额却一直下跌。<br />
　　过去20年中，日本经济停滞不前，国民人均收入在4万美元的水平徘徊。在过去十年中，韩国、新加坡和中国的香港、台湾停留在2万美元水平上。在如此高的收入水平上停滞看起来还不错。但中国情况不同。国家庞大，出口面临挑战，而国民人均收入要低得多。除非改变增长模式，中国经济将在低收入的水平上停滞。<br />
　　快速致富的欲望，控制了中国社会的各个阶层、每个角落和每个人。经济学的逻辑是货币一定要流通；当某些人的钱多时，另外一些人的钱必定要减少。虚高资产价格满足了人们的心理，人们手里似乎拿到更多的钱。泡沫暂时使人们忘记了这个逻辑。这就是为什么一旦条件允许，中国就有资产泡沫。<br />
　　泡沫是对现实的夸张。低成本资金和强劲增长通常是制造泡沫的原材料。过去五年里这两个因素都存在。但是，目前中国完全依靠低成本资金来支持过高的资产价格。目前的低成本资金，一是来自过去出口积累的、存在银行系统的资金；二是由于人民币与美元的绑定，加上美联储美元贬值的政策，“热钱”流入。这两个来源都不具可持续性。美元已经探底。美联储将在2010年提高利率。<br />
　　由于新增贷款增加和出口增加放慢，银行系统的流动性在降低。通货膨胀可能会出现，从而迫使政府收紧货币政策。简而言之，低成本资金环境不可能持续太久。<br />
　　中国出口复苏是从一个低基础开始。复苏将持续到2010年末。但是，低基础复苏与恢复过去的势头不能混为一谈。<br />
　　出于三个原因，高出口增长的阶段已经结束。首先，中国在国际贸易中的份额是中国经济在世界经济份额的2倍。中国继续增加市场份额的可能比较低。第二，增长势头不像以前那样快。在格林斯潘时代，西方消费是由借贷泡沫支持的。这个泡沫已经破裂。未来贸易增长的速度可能是过去的一半。第三，美国和其他西方国家的就业危机，不可避免地导致贸易保护主义。当贸易保护主义的措施针对中国时，其他发展中国家可能抢占中国现有的市场份额。<br />
　　另一可能维持泡沫的办法是将人民币升值，就像日本在“广场协议”后遏制通胀并吸引“热钱”流入。这一战略在中国将不会奏效。日本公司的科技处在世界最前沿，它们可以提高出口价格来部分抵消日元升值的不利影响。中国公司没有这些优势，它们依靠低成本来竞争。日本的战略在中国将失效。<br />
　　在出口拉动经济增长达到高峰后，消费是取代出口成为增长引擎的选择。虽然起点低，但它具有可持续性。这一转变要求政治经济的全面变革。其关键是增加中产阶级可支配资金，降低消费成本。<br />
　　到目前为止，没有任何东亚国家成功完成这一转变。中国还尚未开始这一转变。从目前趋势看，并不乐观。<br />
　　过去十年来，中国一直努力提高居民消费。然而，中国消费在总体GDP的比例年复一年地下降，因为新兴的中产阶级受到政策环境的挤压。房地产价格、汽车价格和收入税率都居高不下。中国的政策，而不是中国消费者或家庭的特质，导致了对出口的过度依赖和消费的萎缩。<br />
　　一个广泛而活跃的中产阶级，是稳定的现代社会的基础。中国的政策正确地照顾了低收入阶层的需求。但是，半市场化的经济为一小部分人提供了投机获利的机会。在未来的中国社会，有极少数超级富裕的阶层，有负担沉重的中产阶级和庞大的、可以支付基本开支的低收入人群。中国正在往这个方向发展。这样的收入结构将注定经济不平衡，而且严重依赖出口。<br />
　　过去十年中国的经济增长，产生了数以百万计的白领工作：经理、工程师、咨询师、会计、律师和银行家等。拥有这样的工作，他们可以买房子、买汽车，和每年一次或两次度假。然而，资产价格上涨的速度超过收入增加的速度，使他们生活在对未来的恐惧中。<br />
　　中国的房地产市场有赢家和输家。但胜负不以教育、经历、工作态度来决定，而是看谁先进入房地产市场。从根本上说，中国经济让投机者获益，而不是努力工作的人们。越来越多的人们意识到这一点，于是投资者越来越多，努力工作人的越来越少。这也是为什么越来越多的公司和个人进入房地产市场。<br />
　　在过去十年中，西方拒绝承认其竞争力下降，并制造泡沫来掩盖它。我担心，中国在未来十年里会重蹈西方的覆辙。在未来两年中，泡沫将越变越大。流动性将很快对资产泡沫不利。置身于泡沫中的多数人总会想：可能是有泡沫，但似乎没什么会导致泡沫破裂，因此它可以继续。泡沫总是要破的，没有例外。越想推迟其破裂，破裂的后果就越严重。过度供给和利率上调都会使其破裂。过度供应是美国泡沫破裂的原因，香港则是利率上调。中国的银行似乎永远在市场低迷时拒绝向开发商提供资金。因此，在市场低迷时，供应将减少，价格调整无法进行。这一人为操控制造了投机心理：政府将永远不会让价格下跌。当投资者认为价格不会下跌时，且银行有足够的流动性来支持，投机的需求是无限的。中国的泡沫将在流动性短缺和利率上调中破裂。<br />
　　最终的杀手就是通货膨胀。原因是印了太多钞票。在印钞和通胀之间有一个时间差。在美国，这个时间差是18个月。中国政府利用补贴来遏制通货膨胀，这个时间差或许更长。它可以是两年。我认为，到2012年中国会经历像上世纪90年代早期那样的通货膨胀。<br />
　　在全球化的时代，通胀也是全球性的。美国的通胀开始抬头。在未来两年，这一趋势将加速。到2012年，美国可能面临通胀危机。美联储将不得不迅速提高利率。如果中国为支持房地产市场而不提高利率的话，资金会从中国外流，银行发生流动性危机。利率最终还是会上升。<br />
　　很有可能，中国的房地产泡沫将在2012年破裂。</div>

							
							
							
							
