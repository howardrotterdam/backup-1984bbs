<br />
我们没有资格批评中国<br />
<br />
主持人：这或许也能对您取得的成功做出一些解释。因为那个时候的社会现实，如果没有人写的话，现代人根本就不会从别的渠道获得了解。1959年《铁皮鼓》出版的时候，在当时的文学界可以称得上是一件“丑闻”。例如，1960年，您曾被授予不来梅文学奖，但因为不来梅市议会的反对，您又被剥夺了这一奖项。这本书曾经被指责为“色情小说”，当然它不是。<br />
<br />
格拉斯：有一些新教年轻人曾经烧过这本书。正如我们刚才说的，50年代是一个复辟时代，我们不应该忘记这一点。在西德，有些人就像纳粹时代的法官一样。比如，他们对《铁皮鼓》进行审判。那个时代就是这个样子。阿登纳内阁主张对书籍进行审查，第一任联邦总统特奥多尔?豪斯也投票赞成这一法案。我在《时代报》上发表的一篇文章也受到了审查。这是那个时代的烙印，现在几乎很难想象。<br />
<br />
说到这里，我想谈一下这次书展的主宾国中国。许多人都批评中国。但是，请不要用一副趾高气扬的态度！联邦总理怎么能够用那样一种态度抨击中国的教育体制等等呢？这只会引起德国人对中国的误解，这是挑衅的、极端的！她怎么能够这样抨击中国呢？！我相信，她也知道，我们自己的民主也并不像我们所希望的一样。（掌声）我读到，联邦总理批评中国的死刑的报道。我倒是更想听听，她哪天批评一下美国的死刑。<br />
<br />
主持人：那么我们是不是最好对此不予评价？<br />
<br />
格拉斯：是的，最好不要说，而且也不是用这种方式和语气来说。如果你要批评什么东西，就必须确实进入到那个环境中去，然后才有资格去评价。</div>

							
							
							
							
