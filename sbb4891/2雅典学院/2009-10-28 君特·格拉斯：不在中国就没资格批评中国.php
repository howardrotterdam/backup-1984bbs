													<h2>君特・格拉斯谈《铁皮鼓》</h2>
						
						
																											<div id="postmessage_304431" class="t_msgfont">我们任由自己被引诱了，被一个专制政体”<br />
<br />
　　问：您也曾经说过，您在纳粹时代也有过不好的回忆，您曾经在党卫军中服役。那么您认为，在您披露这段经历之后，人们会不会异样地看待《铁皮鼓》这部小说呢？<br />
<br />
　　格拉斯：我不这么认为。《法兰克福汇报》出于某种原因对此做了过多渲染，在《剥洋葱》里我提到，最让我感到羞耻的是我当时年幼无知，很多事情都不能明辨是非。比如我的一个叔叔在波兰邮局事件中去世，当时我并不知道事实真相是什么；又或者一位老师失踪了，一位学生有很大的嫌疑，我不喜欢他，所以认为他有罪，而他自己也没有否认，尽管我知道他不可能拥有武器――这些事情都会伴随我一辈子，每每思及此都会顿觉芒刺在背。我一直在忏悔，但很多媒体，比如《法兰克福汇报》以及一些记者的盲目炒作和指责是自相矛盾的，是他们让我在自己的祖国遭人唾弃。<br />
<br />
　　当时我和许多同时代人一样，被纳粹组织所吸引。但我当时并不知道，党卫军是一个罪恶的团体。我很幸运，能在书里把这一切写出来。有一个时刻，我对这个国家感到恶心。<br />
<br />
　　问：当然，读者现在可以说，《铁皮鼓》是这样一本小说，它的作者肯定是对纳粹有一定的了解。它的题材非常具有可塑性，它的语言艺术很伟大。它是一部著作，真实地刻画了纳粹时代。<br />
<br />
　　格拉斯：但我不是一个人。您看看和我同时代的作家们，恩岑斯贝格、马丁・瓦尔泽等，他们都是这样的。我们这一代人被引诱了，或者可以说，我们任由自己被引诱了，被一个专制政体。我经常自问，我们现在生活在一个信息时代，甚至是信息过剩的时代。我从报纸上得知，在60亿世界人口中，10亿人在挨饿。而这只是众多新闻中的一条。我们的社会变得麻木不仁。20年前，民主德国的人们走上街头，打出口号“我们是人民”。但是这种精神现在缺失了。这让我深思，现在这一代人，他们知道那么多，他们获得那么多信息，但是他们却很被动。<br />
<br />
<br />
<br />
《铁皮鼓》书封 <br />
<br />
“这是一个失去了的家乡”<br />
<br />
　　1959年《铁皮鼓》出版，经过10多年的遗忘和经济复兴，纳粹罪行逐渐从德国日常生活中隐去，用格拉斯的话说，社会中充斥着市侩气，“自我封闭又相互排挤”，格拉斯觉得有必要说点什么，于是就有了这振聋发聩的《铁皮鼓》。不过小说出版之后，在德国和美国都受到不少恶评，有人审判这部小说“色情”并当众焚毁，就像在纳粹时期一样，有评论说它，“这本书亵渎神明……故意地、让人讨厌地亵渎神明……阅读《铁皮鼓》就像经历日耳曼人的噩梦一样。”<br />
<br />
　　问：我想回顾一下您创作《铁皮鼓》的年代，您当年的生活是怎样的呢？据我所知，在写作《铁皮鼓》的时候，您的经济情况似乎不太好。<br />
<br />
　　格拉斯：对于经济状况我当时倒是不太操心。我十二三岁的时候就发疯了，因为我决定要成为一名艺术家，这要冒很大的风险。有一次在巴黎，我是拿一幅石板画来支付牙医账单的。我没有现金，为了赚钱，就像古代游吟诗人从一个领主跑到另一个领主那里一样，到处兜售我写的诗。这就是我那时候的生活状况。<br />
<br />
　　说到1950年代的德国，那年代可以说得上是一个复辟时期，那是一个市侩、自我封闭又互相排挤的时代。那个年代人的行为，就像是被恶魔蛊惑了一样。我觉得我必须得写点儿什么。人们会因为各种原因写书，个人的野心、对语言的兴趣、对写作的兴趣等等，当然还有对社会现实的愤怒，我觉得我要对此写一些东西，这也是我写作的原因之一。<br />
<br />
　　问：但1959年《铁皮鼓》出版的时候，在当时的文学界可以称得上是一件“丑闻”。例如，1960年，您曾被授予不来梅文学奖，但因为不来梅市议会的反对，您又被剥夺了这一奖项。这本书曾经被指责为“色情小说”，当然它不是。<br />
<br />
　　格拉斯：有一些新教年轻人曾经烧过这本书。正如我们刚才说的，1950年代是一个复辟时代，我们不应该忘记这一点。在联邦德国，有些人就像纳粹时代的法官一样，比如他们对《铁皮鼓》就进行了审判。那个时代就是这个样子。阿登纳内阁主张对书籍进行审查，第一任联邦总统特奥多尔・豪斯也投票赞成这一法案。我在《时代周报》上发表的一篇文章也受到了审查。这就是那个时代的烙印，现在几乎很难想象。<br />
<br />
　　说到这里，我想谈一下这次书展的主宾国中国。我也知道，许多人都要批评中国。但我们联邦总理怎么能够用那样一种态度来抨击中国呢？这只会引起德国人对中国的误解，这是挑衅的、极端的！至少中国取得的成就要多得多，我相信，她也知道，我们自己的民主也并不像我们所希望的一样。<br />
<br />
　　问：那么我们是不是最好对此不予评价？<br />
<br />
　　格拉斯：是的，最好不要说，而且也不是用这种方式和语气来说。如果你要批评什么东西，就必须确实进入到那个环境中去，然后才有资格去评价。如果你想干预别人的事情，想要批评别人，那么你必须首先管好自家门前的事情，这样你说出来的话才有人信。这就是我说的有个自家的“篱笆”，新闻工作者也要好好学学如何扎紧自己的篱笆。要去伪存真，看到事实的真相。<br />
<br />
　　奥斯卡拒绝生长。他保持着孩子般的样子，矮小、看起来很单纯。奥斯卡作为一个小人可以在战争中幸存，但是他无法逃避罪责。他促成了他妈妈的死；他对他舅舅（他的生身父亲）的死也负有责任，他还导致了他的父亲因为被纳粹党徽章卡住喉咙而死于苏联士兵的枪下。战争之后，奥斯卡终于在一个运货车厢里继续生长了。一个拥有超自然能力的鼓手。奥斯卡对成长的拒绝被认为是国家罪行的象征符号。<br />
<br />
　　问：《铁皮鼓》从问世到现在，大家一直在读它，您怎么看待这部小说拥有如此巨大的影响力？<br />
<br />
　　格拉斯：这个问题有点让我吃惊。美国出版商福尔马茨・布吕和德国出版商库尔特・沃尔夫将我请到了瑞士苏黎世，库尔特・沃尔夫曾对我说：“我非常喜欢您刚在德国出版的《铁皮鼓》这部小说里表达的思想，我想先在美国出版这本书。您觉得美国读者能读懂它吗？”我说：“不。这个故事发生在外省，发生在但泽，确切地说是但泽市郊，书里的人说的都是方言，所有的事情都在这么一个小地方发生。如果巴伐利亚人能读懂这本书，我就已经很开心了。”我对他说的那句话印象深刻：所有有意义的文学作品都是立足于当地生活的。这本书先是在美国，后来又在40多个国家出版。<br />
<br />
　　但时至今日，读者对我作品的兴趣都还集中在《铁皮鼓》上。1956年，我开始在柏林写作《铁皮鼓》，之后又创作了《猫与鼠》和《狗年月》。对于我来说，这三本书属于一个整体。我觉得，《狗年月》是《铁皮鼓》的一个提升。不过现在谁愿意听作者说话呢？《铁皮鼓》是一部用第一人称叙述的小说，主人公是一个流浪汉形象，这是一个那个年代的小人物，他的谎言、他的野蛮、他的梦想、他所有的可能与不可能都在书里出现。《铁皮鼓》中为数众多的配角都以这个主角为中心。而《狗年月》有三个叙述者，这是一本复杂得多的小说，真相变化无常，事实与事实之间相互对立，读者要从中作出自己的判断。当然，这是作者的评价。对于我来说，《狗年月》是这三部小说的一个完结，《铁皮鼓》则只是一个开始。对我来说这三本书是一段独立而自成一体的写作过程.。<br />
<br />
　　问：奥斯卡在3岁的时候停止了生长，这是一个非常特别的人物形象。他是一个局外人，能够从一个特别的角度观察这个世界，能够准确地看待当时纳粹统治的环境以及小市民的生活圈子，看到了纳粹环境的怪诞、荒谬。<br />
<br />
　　格拉斯：1950年代早期，我那时候是一个艺术系的学生，第一次出国旅游，去了意大利，然后去了法国，并且写了一首很长的诗来描述在那里看到的一排圆柱。如果一个局外人从外部观察一个社会的话，他看到的就是这样一排柱子。奥斯卡是从一个特殊的角度来看的，他从桌子底下观察这个世界。虽然他停止了生长，但他一直在经历事情。<br />
<br />
　　我很喜欢他关于马戏团的经历，马戏团是《铁皮鼓》里的重要情节。马戏团是梦想与现实直接相连的地方，也是流浪汉小说也是欧洲小说的重要形式，通过主人公的流浪生涯来反映他的时代。拉丁美洲所谓的“魔幻现实主义”文学里也有流浪汉小说的内容。在德国，人们有意识地去读、去写这种题材。法国启蒙运动时期的伟大作品，如伏尔泰的《天真汉》或者狄德罗《雅克和他的主人》等都是遵循流浪汉小说的传统而创作的。在路德翻译《圣经》和格里美尔豪森之间的一个世纪，有一位伟大的作家，把拉伯雷的《巨人传》翻译成了德语。不仅是翻译，他还进行了再创作。格里美尔豪森读过这一版本的《巨人传》，并参考了它的结构。这位17世纪的伟大作家博览群书，并且很关心在欧洲的其他部分发生着什么，也许他也会关心法兰克福书展上都有些什么。<br />
<br />
　　格拉斯1927年生于但泽，如今他也是这座波兰小城的荣誉市民。这座城市是格拉斯最重要的小说故事背景，《铁皮鼓》、《狗年月》和《猫与鼠》合称为但泽三部曲。但泽是一座充满历史诡异的城市，这座原属于东普鲁士的港口城市因为一战德国战败被划分为自由市，实际上就是划给波兰。1939年，希特勒重占但泽，二战随即爆发。纳粹战败后，但泽重新被划为波兰所有。这样一座被历史戏谑的城市成了老乡格拉斯写作的舞台。格拉斯与赫塔・穆勒，两位德国诺贝尔文学奖得主，都来自德意志民族的历史创伤之地。<br />
<br />
　　问：您刚才也指出过，《铁皮鼓》对您来说也可以说是一个“负担”，您从来不说“我最好的小说”。但是我知道一个批评家，也许您也认识他，他曾说过：“我在格拉斯的作品里找不到比《铁皮鼓》更好的小说。”<br />
<br />
　　格拉斯：他要以此谋生嘛。但是我靠别的东西活着。如果你们也写过东西，你们会有体验。要写作一部小说，一个虚构出来的故事，不是一件简单的事情，你要坐在它面前，要把握一片很大的区域，害怕文字会污染洁白的纸张。我的每一本书都是这样开始的。有的作家发现一个题材，然后就会把它当作自己毕生的题材来写作，你们都知道这样的作家，我也都是这么过来的。每本书都有一个新的开始，也许可以说，一本小说就像一个广阔的世界。《铁皮鼓》就是这样，它严格地围绕但泽展开。<br />
<br />
　　问：您提到了但泽，这是我接下来想问您的。这座城市，它独特的氛围，它位于德国和波兰的边界。对您来说，《铁皮鼓》在某种意义上也是一部乡土小说。<br />
<br />
　　格拉斯：这是一个失去了的家乡。如果你失去了什么东西，你会强迫性地去回想它。而文学是很神奇的东西，它提供了这么一个可能性，把某些绝对已经失去了的东西用写作的手法再现、重生出来。这三部小说就是这样。<br />
<br />
　　但是《铁皮鼓》比起其他两部来说，对于但泽，也就是今天的格但斯克，有着更为重要的作用。这本书在波兰曾经是被禁的。共产主义时代的波兰曾经把划分过去的德国东部省份的历史篡改过，将它描述成本来就属于波兰的。德国在那里的历史完全被抹杀了。原先的但泽人流落到德国各地，其他地方的波兰人来到这里，第二代、第三代人在但泽出生。对于他们来说，《铁皮鼓》讲述了但泽早先的历史。<br />
<br />
　　问：人们可以在这本书里找到与故乡的紧密联系。您也曾经说过，有很多书的主题都是波兰。人们抱怨一片失去了的土地。<br />
<br />
　　格拉斯：是的。这点我曾和BBC的萨鲁曼・罗塞蒂谈论过，有一段时间由于犹太人的关系德国人是被禁止前往当地的，我们俩当时一致认定，它属于一种遗失的东西，因此要求一种“占有性”的写作，因为文学的魅力就在于可以通过作家的笔触将已经消失的东西重新描摹得栩栩如生。<br />
<br />
　　我的祖父曾经坐在整理好的行李箱上等待，因为阿登纳在竞选时答应过，他们会很快回家。这是那个时代最大的竞选谎言之一。我知道这有多痛苦，但我也知道，我失去了它。我们永远失去了它，因为我们的错误。然后我把这一点说了出来。1970年代威利・勃兰特在华沙下跪，我也起了作用。这是战后联邦德国和波兰的第一次沟通。三天后在但泽爆发了大规模的起义。我的小说《比目鱼》就是为此而写的，当时写作一部波兰小说是不可能的。德国文学对于波兰的当代历史总起着一种尴尬的作用。<br />
<br />
　　（感谢译林出版社编辑王蕾提供翻译支持）</div>

							
							
							
							
