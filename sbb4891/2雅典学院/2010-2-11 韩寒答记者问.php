之前不开是因为是因为接受不了每一篇深情款款的博客文章的最后冒出一句描红加粗的<br />
我最近在玩新浪微博，很酷，很新潮。就像演出完美结束刚谢幕就坠台了，比赛顺利完成刚冲线就撞墙了。<br />
 <br />
 <br />
2：你说现在中国流行拜金主义很好，为什么？（内媒）<br />
因为现阶段的中国是一个没有信仰和缺少理想的国家，大家都很现实，很多年轻人的理想就是房子一套。大家都只想着要有钱。一个国家完全可以用几十年去寻找信仰，在这个时期里，没有信仰，一定比有着错误的信仰要强。<br />
 <br />
3：但是拜金主义导致了毒奶粉等东西的横行，危害了国家（内媒，这个问题很聪明）<br />
你转移了，毒奶粉的造成是因为政府监管不力。如果“想赚钱”和“做坏事”之间往往都能联系起来，那只能说明司法失职，而不是什么主义不主义，思想不思想，文化不文化，信仰不信仰的问题。<br />
 <br />
4：从最近网上的一些言论来看，好像中国人都很反对美国，是这样的吗？(外媒)<br />
你可以把那些人召集起来，免费发发绿卡看看什么效果。事实上，至少我知道的现在那些接触资讯比较多的年轻人，他们都能保持自己独立的判断，中国操蛋的时候，他们认为中国操蛋，美国操蛋的时候，他们一样认为美国操蛋。这样的人越来越多，说话才越来越有意思。以前的中国，大家信奉的是，首先我是一个中国人，其次我是一个某地人。第一句对外，第二句对内，骂成一团。现在的中国年轻人，他们已经慢慢明白了首先我是人，其次我是一个中国人。包括我的父母，事实上在国际问题上，他们总是一个亲美，一个反美，但并不妨碍他们的性生活并把我生了下来，我生下来以后保持中立。这样不很好么？<br />
 <br />
 <br />
5：你认为中国没有好的文化作品是因为中国审查的尺度太严格，但是很多国家对世界有影响的好作品都出自于这个国家并不完全自由的时期，而很多全自由的国家反而没有好的文学作品涌现，你觉得呢（来自外媒，这个问题很好）<br />
 <br />
事实上，你说的很对。没有审查不一定能出好作品，但特别严格的审查一定出不了好作品。除非不想在国内混了。一来中文存在翻译的问题，比如拉丁语系国家，这个国家不让我出版，我可以在其他国家出版并且获得世界上的成功，情况有所不同。二来就像作曲，很多国家的作曲家有哆来咪发少拉稀，但我们国家只有少拉稀，网络时代到来以后，逐渐放开一些，有了咪发少拉稀，不是不能写出好曲子，但难度肯定增加了。因为每一个作家，每一个导演，每一个编剧在落笔的时候都会想，我这段内容会不会通过过审查。所有的编剧都知道一个信条，那就是市长可以是坏人，但市委书记一定要是好人，而且最后纪委一定要把坏人抓走。在物价和生活压力这么大的年代，大家势必更加小心翼翼，生怕龙颜大怒，自己丢了饭碗。当然，这也是中国比以前进步的地方，之前的中国很多人因言或罪，现在的中国最多就是让你禁言，比较少获罪。<br />
比如说以下知名电影都是中国导演拍的，《肖申克的救赎》，我想他在中国就不能通过审查，第一，中国警察怎么会抓错人呢？第二，就算抓错了怎么还能让你跑出去呢？第三，监狱长怎么可能是坏人呢？不予通过。<br />
比如说电影《教父》，不用说了，中国怎么可能有黑帮。不予通过。<br />
比如说电影《蜘蛛侠》《蝙蝠侠》，中国的警察怎么可能搞不定，不予通过。<br />
比如说电影《牯岭街少年杀人事件》，建议改成《牯岭街成人杀人事件》，并且主要要反映我民警如何机智迅速的破案。<br />
比如说电影《杀人回忆》，警察到最后怎么能连凶手都没有找到，不予通过。<br />
