<br />
首先应调整社会分配体制，解决好民生问题。让一般人通过正常劳动可以买得起房，起码有买得起的希望。要做到这一点，一是要切实保障个人的基本权益，建立各种利益的表达机制；二是要约束强势集团的掠夺行为，包括热衷卖地的地方政府和谋取暴利的房地产商；三是要加快各类社会保障性住房建设。另外，还需要加强对社会边缘人的物质救济和精神帮助，使他们能有尊严、有希望地生活下去。<br />
<br />
其次，应采取各种办法，使社会各阶层之间更容易流动、更有规则地流动。只有合理化流动的加强，才能消解官民之间、贫富之间、强弱之间的“我们―他们”式的结构对立。尤其需要保障底层知识分子的基本物质权益。中国数千年来的历史进程表明，他们往往具有相当的号召力、影响力和破坏力，这一群体的上升通道尤其需要重点关注。<br />
<br />
再次，应加强心理干预。南平惨案的背后，一方面需要挖掘背后的社会问题，另一方面还需要剖析郑民生个人的人格、心理问题。建立社会心理发现和干预渠道已到了迫在眉睫的地步，对弱势群体的关怀和心理引导必须进行重点强调。可以对重点人群提供免费或低价的心理治疗。还应该加强社区建设，使弱者能找到归属感。社区是社会的最基层单位，加强社区为代表的基层社会建设，不仅让它成为我们生活的家园，更要成为心灵的家园。通过社区工作者建立一种心理疏导的机制，能及时发现问题并对社会情绪进行疏导，对社会心态进行引导，对一些反社会行为进行干预。使从源头解决问题成为可能。<br />
<br />
除此之外，更重要的是约束公权力。正常的社会也不能完全避免反社会的“杀人狂”，但不正常的社会，会逼得更多正常人走极端。要想真正限制公权力过强，笔者认为应做到四点，即明晰的产权、有权威的司法体系、真正的代议制度和开放的媒体，而这在现行宪法框架下完全可以做到。应该让每个公民的努力和付出都能有适当的回报，让遵守规则者得利；让每个公民都知道宪法是社会的底线，从而可以合理地预判自己行为的后果；让公民通过结社等争取共同利益；在公民自由表达的基础上建立多元价值观。这样才能降低民众因不确定性而产生的恐惧，避免南平血案式的恶性事件。<br />
<br />
总之，南平血案的发生及其所暴露扭曲的社会心态，呈现出病态性的社会心理疾病，也意味着中国进入了一个社会心态更复杂的阶段。如果不及时调整和积极处理，必然会影响到和谐社会进程，甚至发生一些大家都不愿意看到的事情。而要让弱者免于生活无着的恐惧、免于被欺辱蔑视的恐惧、免于被社会抛弃的恐惧，让普通人免于成为无辜受害者的恐惧，需要社会各方面的努力。</div>

							
							
							
							
