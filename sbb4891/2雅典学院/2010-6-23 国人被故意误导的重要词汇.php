　　其实作为高等学校的录取考试，几乎全世界都有，古代社会水平高点的师傅收徒弟也是要考察一下徒弟的底子如何，水平太差也是不教的，因为这样教这样的徒弟，付出的心血多，时间多，成本高，而且往往还出师后，本事也不高，其他准备拜师的人看到，就会说这师傅教的不好，教出来的徒弟都这么笨，结果做师傅的还丢了名声。所以考试制度的存在是一件合理而自然的事情，其差异在于自由民主的国家，其考试内容是由独立机构决定的，官府不能干涉考试内容也不能干涉学术研究和教育内容，甚至在自由民主做的好的国家，整个考试完全是由民间的组织进行，比如美联邦的高考SAT就完全是个民间非营利组织举行的考试，官府无权干涉考试内容，布什也不能把它的D纲拿来考试，答案也不能由布什来定；而在砖制毒裁国家的考试则完全是由官府举办的，学术不能独立，教育不能独立，考试由官僚垄断，内容完全由官府来决定，答案也是官府强加指定的。这就是两种考试制度的本质区别，一个完全彻底的官办，一个是独立于官府的考试或者民办考试。所以基国应该取缔的是官办垄断的高考，改为自由民主的独立高考或者民办高考，而不是不要考试。<br />
<br />
<br />
　　所以经常有良心的有识之士，当意识到基国“高考制度”不好时，呼吁取消“高考制度”时，因为还在使用这个具有欺骗性的砖制狼奶语言词汇“高考制度”，所以不管怎么给大家解释，大多数民众也很难理解和接受。这就如同呼吁取消“户籍制度”，民众是很难理解一样，但是一旦朋友们使用揭露本质的“户籍出身歧视制度”这样的自由民主语言体系词汇，呼吁“取缔户籍出身歧视制度”的时候，问题一下就明朗了，民众一下就理解了。所以说你用砖制狼奶语言体系说半小时，赶不上使用自由民主语言体系说一个词。所以追求自由民主的朋友们不要使用遮蔽罪恶本质的基国的“高考制度”这样的砖制狼奶语言体系说法，应改用自由民主语言体系说法，取缔基国的“官僚垄断高考制度”或“官办垄断高考制度”，实行自由民主的独立高考制度或民办高考制度。同理也应取缔基国的“官僚垄断教育制度”或“官办垄断教育制度”，实行自由民主学术独立的独立教育制度或民办教育制度。<br />
<br />
<br />
　　最后顺便说一下基国现在的所谓“民办学校”，这是一个完全欺世盗名的狼奶语言体系词汇。基国的这种所谓“民办学校”招多少学生不能由学校自己决定，要由官府的官僚定指标，指定学生人数；用什么教材不能由学校自己决定，要由官府指定教材；该收多少钱不能由学校自己决定，要由官府的官僚来指定价格；甚至学校该请什么样的老师，也要由官府的官僚来定老师，也就是说一个正常民办学校应该有的几乎全部经营自主权都没有，这根本不是什么真正意义上的民办学校，这只不过是打着民办幌子的官办学校，其正确称呼应该叫“假民营的官办学校”<br />
<br />
<br />
　　只有同时取缔官办垄断考试制度和官办垄断教育制度，改为学术独立的，真正的民办教育和民办考试制度后，才能真正解决砖制毒裁社会教育成为官府愚民工具这个核心问题。才有可能在这个基础上产生出真正对我们老百姓有利的更公正的教育制度――比如教育券制度等，说简单一点就是把满足基本教育的钱以教育券的形式发给受教育者或者他的家长，然后家长拿着教育券，到各个独立办学的学校之间去自由比较选择，然后把教育券作为学费给学校，最后学校拿着教育券到政府或者银行去兑换成货币，这样的教育制度，既保证了教育学术的独立，有保证了学校的竞争，降低了成本，提高了效率，而且也保障了基础教育的公平，更是取消了官办教育中对的官僚渔民教育，和官僚高干特权教育享受。<br />
&nbsp;&nbsp;<br />
　　27、根本没有资本剥削，只有官僚剥削<br />
<br />
<br />
　　马克思的资本剥削完全瞎说<br />
<br />
<br />
　　先从市场公平讲起。<br />
<br />
<br />
　　老板和员工之间自愿签署劳动协议，这是公平交易。<br />
<br />
<br />
　　剥夺员工联合起来组织工会和老板进行集体谈判的权力，这就是不公平交易。<br />
<br />
<br />
　　集体谈判可以大幅度提高员工和老板之间的收益分配比例，现在基国员工和老板之间的收益分配比例约是1：3，每挣100块钱中，员工拿25块，老板拿75块，而员工有集体谈判权力的自由民主国家这个比例是4：1，即每挣100块钱中，员工拿80块，老板拿20块，可见在官权大，民权小的官权社会中，比如基国确实存在剥削，可在自由民主的民权社会根本没有这个剥削存在。所以说剥削来源于官府的官僚对民权的剥夺。<br />
<br />
<br />
　　一个老板可以做电信运营商，另一个老板也可以做电信运营商，这是公平的市场。<br />
<br />
<br />
　　只准某老板开网吧，其他老板被剥夺开网吧的权力，这是不公平的市场。<br />
<br />
<br />
　　剥夺老板参与竞争的权力，这种不公平的市场也会导致一些剥削出现，因为不管你工人如何集体谈判提高工资，但是由于没有其他老板和他竞争，因此他就可以通过不断提高产品卖价获得更高利润，从而继续维持老板和员工之间的收入分配比例。甚至老板根本就不会和工人集体谈盘进行妥协，因为这个老板没有竞争压力。比如一个做冰箱的A老板，如果不答应工人的集体谈判条件，工人就会集体罢工，在有和没有其他老板参与竞争的两种情况下，A老板的态度是截然不同的。<br />
<br />
<br />
　　如果有竞争存在，其他的B老板生产的冰箱就会占领市场，罢工会使A冰箱厂生产停滞，赚不到钱，而使B老板大赚特赚，罢工会给A老板带来巨大的损失，甚至破产，最终被淘汰出冰箱市场，所以说只要答应员工集体谈判的条件后，还有点钱可赚，不致亏本，A老板都是会答应的，而且他也无法通过提高冰箱销售价格继续维持和员工之间的收入分配比例，因为有其它的B老板生产的冰箱和他竞争。所以说只要不剥夺其它老板参与竞争的权力，老板就根本无法剥削员工。<br />
<br />
<br />
　　但是如果剥夺了其他老板参与竞争的权力，A老板就根本不怕员工联合起来集体谈判，因为冰箱市场上人们还是需要冰箱，只是暂时买不到，不会有人抢夺他的市场份额，只是暂时赚不到钱，以后恢复生产了。需要冰箱的人还是要买A老板的冰箱，市场还是A老板的，钱还是照样赚。所以说保障老板参与市场竞争的权力，就是保障员工的利益，也就保障了员工不被剥削。<br />
<br />
<br />
　　综上所述，大家可以得出一个结论：老板和员工之间根本不存在所谓的“资本剥削”，如果出现了剥削，那么必然是因为官府的官僚剥夺了老板和员工的某些权力导致的，所以说只存在官府的官僚阶级对民众的“官僚剥削”，而根本不存在所谓的&quot;资本剥削&quot;。<br />
<br />
<br />
　　其实中国历史也早就告诉我们，从来都是“官”逼民反，从来也没听说过什么“地主”逼民反！中国的历史也证明了――官僚阶级对民众的官僚剥削才是问题的根本。资本剥削纯粹是马克思瞎编乱造的邪说，所以追求自由民主的朋友们，请抛弃“资本剥削”这个砖制狼奶语言体系词汇，改用自由民主语言体系词汇“官僚剥削”，因为任何时候当你发现有剥削的时候，其实深入研究下去都是官僚在搞剥削。</div>

							
							
							
							
