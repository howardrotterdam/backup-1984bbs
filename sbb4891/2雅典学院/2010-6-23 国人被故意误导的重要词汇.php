1、“苏国”和“美联”。<br />
<br />
<br />
　　追求自由民主的人们，我们都是从小被用狼奶灌大的人，所以写反对狼的文章也经常拿狼奶来写，这怎么能写得好呢？写自由民主的文章首先就是要停止使用狼奶语言，哪些是狼奶语言呢？举个例子――“苏联”和“美国”，这就是典型的狼奶语言，正确的语言体系应该叫“苏国”和“美联”。因为苏国是真正的集权国家，美联才是真正的联邦国家，而狼奶语言体系完全遮蔽了这个真相。几乎与政治有关的所有词汇基本都是如此。下面我一条条分析。<br />
<br />
<br />
　　2、&quot;邦国&quot;<br />
<br />
<br />
　　“州”这个词在古代汉文明中，中央集权世袭皇帝制/度中曾经存在过这个行政编制，是一个比现在的省大一点的行政区域范围，管理官员由中央集权头头指派，这个砖制狼奶语言体系词汇完全扭曲了组成美利坚合众国的这些“邦国&quot;，美联的这些邦国的领导都是各邦国自己决定的，就是当年组建“合众国”也是13个国家自愿联合成立的，就和现在欧盟走向大联邦是一样的。所以大家停止使用这个根本不能表达本意的砖制狼奶语言体系词汇“州”，而应该使用自由民族语言体系词汇 ―― &quot;邦国&quot;<br />
<br />
<br />
　　3、“白房子”<br />
<br />
<br />
　　下面再分析一个狼奶语言体系词汇“白宫”，其实正确的自由民主语言体系应该叫“白房子”“白房”，因为它根本不是代/表砖制不平等的什么“宫”，是“white house&quot;,不是“whitepalace”。<br />
<br />
<br />
　　4、联邦首席执行官<br />
<br />
<br />
　　上面说到美联邦的那个白房子，现在就说说在这个白房子里面办公的那个人，在狼奶语言体系中这个人叫“总统”，哇！好厉害，是“总”了，还要“统”，听起来像是总揽一切大权、统治一切，比皇帝还厉害，至少差不多。<br />
<br />
<br />
　　其实呢，这个在狼奶语言体系中听起来大的不得了的“总统”，既不能制定这个国家任何法律，也不能按法律裁定任何事情，即：既没有立法权，也没有司法权，甚至连他“下面”任何一个邦国、任何一个城市的领导都管不着，你说他还总统个什么，他仅仅有按照法律执法的权力而已，并且了解美联法律的都知道，他们的法律对这个“总统”具体执法方式也有很多限制，所以他压根不是什么“总统”，而仅仅是个“联邦首席执行官&quot;而已，追求自由民主的人们应该停止“总统”这个砖制狼奶词汇的使用，改用“联邦首席执行官&quot;，或者简称美联“执行官&quot;。<br />
<br />
<br />
　　5、立法院<br />
<br />
<br />
　　说了美联邦的那个首席执行官，我们知道他仅仅是个执行者，根本不是狼奶语言体系中的“总统”，那么我们又看看他在执行什么呢？了解后才知道，他们执行的是个叫“议会”的机/构制定的法律或法案，其实这也是个狼奶语言体系词汇，因为“议会”听起来和朝廷的朝会差不多，有人说，不都是开会吗，有何错？错误就在于这朝廷开会，不仅立法，也司法，还执法，还任命各级官员，说简单点就是天下事管完了，可是美联那个“议会”既不能去司法，也不能执法，更不能任命美联邦各邦国的官员，甚至连任命美联的那个执行官都不能，可见这个狼奶语言体系词汇“议会”完全掩盖了这些真相，事实上那是个管“立法”的机/构，所以追求自由民主的朋友应该停止使用砖制狼奶语言体系词汇――“议会”，而改使用正确的自由民主语言体系词汇“立法院”。<br />
&nbsp;&nbsp;<br />
　　6、临时公民审判团”或简称“公民审判团<br />
<br />
<br />
　　讲了美联执法的首席执行官，和立法的立法院，现在讲一讲这个世界几乎最自由民主的国家的司法机/构――法院，据说在他们的法院里面有“陪审团”，我们的法院里面也有个陪审员，我们那个倒确实是“陪”的，知道的都明白那是个陪衬，不起作用的花瓶，用“陪审”来描述我们法院那个确实很贴切，可用描述砖制制/度中的东西去描述自由民主体系里面的东西这就完全是诬蔑和诽谤，因为这个狼奶语言体系的“陪审团”在美联的法庭上具有最大的权力――“裁定罪名是否成立”。<br />
<br />
<br />
　　具有这么大的权力的东西怎么是“陪审团”和什么“陪审员”呢！很明显这些词汇遮蔽了其本意，正确的自由民主语言体系词汇应该叫“临时公民审判团”或简称“公民审判团”，“临时”是为<br />
了防止老百姓被洗脑，和长期担任该职务后变成政府利益集团的一分子，“审判”说明其拥有实权，完全没有“陪”的意思。美国的法庭里面的法官仅仅是个“法庭的主持人”，没有裁定罪名成立与否的权力，“团”说明其有多人组成<br />
<br />
<br />
　　7、“立法院主持人”<br />
<br />
<br />
　　说了砖制狼奶语言体系词汇“议会”，我们知道用自由民主语言体系，应该叫“立法院”，那么我们继续来研究砖制狼奶语言体系词汇&quot;议会主/席&quot;，听这名字，这官好大，把议会都管完了，这就是狼奶语言体系词汇取名的妙处，无时无刻不在混淆概念，让你以为这自由民主开的会和砖制的天朝朝廷开的朝会一样，都有个老大一言九鼎，实际上了，这个所谓的“主/席”仅仅是个会议主持人，没有多大权力，甚至没有投票权，仅仅在投票成为平局的情况下才会有一次表决资格，就是辩论的时候，甚至没有辩论资格，他就是个主持人而已，所以我们要抛弃这个砖制狼奶语言体系词汇“议会主/席”， 改用自由民主语言体系词汇“立法院主持人”。<br />
&nbsp;&nbsp;<br />
　　8、“法庭主持人”<br />
<br />
<br />
　　前面讲了自由民主语言体系词汇――“临时公民审判团”或简称“公民审判团”，知道了在美联法庭上，那个所谓的“法官”所起的全部作用就是维持秩序，也就是当双方在对抗的时候，维护“游戏规则”。<br />
<br />
<br />
　　双方的证据是否可以呈堂，提出的证人是否可以出庭，向证人的提问是否恰当，在法庭上可以说什么，不可以说什么，在有一方犯规的时候叫停，等等，这些都是所谓 “法官”的责任。但是，真正在案子里最终决定输赢的，却不是这个所谓的“法官”。在审判中，所谓法官只是个球场上辛辛苦苦监视双方是否犯规的。而且在整个审理过程中，他确实在不断地吹哨叫停。法官的水平一是体现在对于游戏规则的熟悉，还有就是对抗衡的双方“吹哨吹得公正”。<br />
<br />
<br />
　　他的水平绝不是体现在给被告定罪时能够“明察秋毫”。在这些案子中，美联的法官并不是断生死的“青天大老爷”，断案根本就不是他的事儿，他也压根儿就没那份权力。那么，最终到底是谁在掌握被告的生杀大权呢？是最最普通的美联平头大百姓，即“临时公民审判团”或简称“公民审判团”。<br />
