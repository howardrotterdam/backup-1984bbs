 <br />
　　15、自由民主<br />
<br />
<br />
　　下面是个重量级砖制狼奶语言体系词汇――“资本主/义”（capitalism），一个很简单的例子就能很好的说明这个狼奶词的欺骗性，你可以看到那些被叫做“资本主/义”国家的人民自豪地说“我以我们的国家是资本主/义（capitalism）国家而自豪”,如果说这句话的人是当老板的，我倒觉得还可以理解，问题是普通的打工仔也这样说，这就让人觉得不可思议了，你一个打工仔，一个资本都没有，你为“资本主/ 义”自豪什么呀！！！显然“资本主/义”这个词欺骗了我们。<br />
<br />
<br />
　　我们平时说“某某主义”，就是某某东西在某种思想中具有最“主”要的意“义”，即“主义”，说通俗点就是某某东西在某种思想中是老大的意思，那我们来看看“资本”（即生产资料）在自由民主社会是老大吗？<br />
<br />
<br />
　　首先如果没有劳动力，任何生产资料都没有价值，反过来看让劳动力脱离生产资料，劳动力也一样几乎没有价值，现代社会谁也离不开另一方，双方是对等的，大家都必须结合在一起才有意义。<br />
<br />
<br />
　　其次双方的价格是在双方自愿的的情况下在竞争性的市场中决定的，提供劳动力的有其他劳动力和他竞争（基国的官办垄断企业里面，工作轻松，收入高的都是有背景的，其他人是不能去竞争的），提供生产资料的有其他老板和它竞争（基国通过各种执照打压竞争，各种权力垄断随处可见），甚至还有现在打工的作为潜在的老板和它竞争（基国的很多行业连其它老板都不能染指，更别说普通打工者了），而且还有反垄断法在保护生产资料之间的竞争（基国没有真正的反垄断法，官僚垄断随处皆是），还有工/会在帮助劳动力和生产资料进行集体谈判（基国没有真正工/人自己的工/会），总之人人都可以选择当老板或者打工者，人人都能提出自己想要的回报的价格，不管是打工回报，还是投资回报，但谁也不能强买强卖决定价格，因为有市场上的其它竞争者存在。<br />
<br />
<br />
　　可见资本和劳动力之间谁也不占决定性的优势，现实的自由民主社会投资收入和劳动力工资之间的分配比例约为1：4，资本投资也不占什么决定性的优势。可见把这样的社会叫做“资本”主义社会完全是个陷阱，把代/表自由平等民主的社会体系中的一部分――“资本”作为这个体系的最核/心的东西取名为“资本主义”是对这个制度的诬蔑，是对这个体系某方面的刻意放大和丑化，所以大家应该抛弃这个砖制狼奶语言体系词汇“资本主义”。<br />
<br />
<br />
　　改用“自由民主”来替代它。因为自由民主才是这个社会最重要的特征，包括经济上的自由 ――自由投资、自由打工、自由竞争；和政治上的自由、平等、民主。以此类推，“资本主义社会”应该改称“自由民主社会”，“资本主义国家”应该改称“自由民主国家”，“资本主义制度”应该改称“自由民主制度”.<br />
 <br />
　　16、 民有制<br />
<br />
<br />
　　下一个砖制狼奶语言体系词汇是&quot;私有制&quot;的使用.按道理“私有制”这个词不应该是我们批驳的对象。可是我要说，选择这个砖制狼奶词汇来描述自由民主制度的核心制度之一是经过巧妙安排的，而且这种安排对自由民主一方是极其不利的。<br />
<br />
<br />
　　大家可以把“私”和“公”这两个词放在一起对比以下，很明显“私”具有某种贬义，“公”具有褒义；我们再看“官”和“公”这两个字，很明显“官”具有贬义，“公”具有某种褒义；然后我们再看“私”和“民”这两个字，很明显“私”具有贬义，“民”具有某种褒义；砖制巧妙的选择了“公”有制来美化“官”有制，同时选择了“私”有制来丑化“民”有制，而且不断地通过垄断的官办媒体妖魔化“私”这个字眼。<br />
<br />
<br />
　　其实“私”有制的企业在基国现在也存在，但在说道这个概念的时候，大家可以注意基国是怎样表达的，他们说的是“民”营企业，绝大部分情况下都说的是“'民’ 营企业如何如何为社会作出了贡献”和而很少说“'私’营企业如何如何为社会作出了贡献”，也就是说，用的着它的时候说的是“民”，否定它的时候说的是“私 ”，大家注意到这里面的微妙差别呢吗？注意到了，这说明你懂了，所以追求自由民主的人们，除了在学术领域，其他时候请回避使用“私有制”，而是用更能争取人心的词汇“民有”“民有制”或者“公民个人所有制”，同理“私营企业”应该改为“民营企业”/“民办企业”，好!学到这里,我们再和前面学过的“国营 ――官办；国有――官有/官办；国营企业――官办垄断企业；国有企业――官有垄断企业/官办垄断企业；国有银行――官办银行；国有制――官有制”这些知识一起来看下面的句子：<br />
<br />
<br />
　　“官办企业越多越有利于官府，对咱老百姓越不利；民办企业越多越有利于咱老百姓，对官府的官僚越不利”。这句话是不是很容易说服人？但是如果你使用砖制狼奶语言体系可以安排的词汇“国有企业”和“私有”再试试：“国有企业越多越有利于官府，对咱老百姓越不利，私营企业越多越有利于咱老百姓，对官府的官僚越不利”。发现这两种语言体系的区别呢吗？<br />
<br />
<br />
　　所谓的“资本主义”国家其实是信仰自由的，所以说所谓“资本主义”作为一个意识形态根本不存在。他是其敌对势力虚构出来的假象敌人。<br />
<br />
<br />
　　17 、官权主义”<br />
<br />
<br />
　　下面说说“社会主义”，这个词仍然是狼奶语言体系的词汇，这种“社会主义”的说法对砖制欺骗极其有利，因为它把其本质隐藏了起来，所谓“社会主义”其实就是“集体主/义”的意思，把“集体主义”扩大到整个“社会”既是所谓的“社会主义”，那意思就是说几乎所有的事情，或者说绝大多数事情都要由这个所谓的“ 集体”或所谓的“社会”来决定。而实际上行使这个“集体权力”或者“社会权力”的其实是官府，所谓的“集体主义”和“社会主义”其实就是“官府主义”或者说“官僚主义”，是一切服从官府，当官的就是老大，当官的控制一切的制度，这个砖制偷换概念的方法其实和砖制用所谓的“公有制”偷换“官有制”的方法如出一辙，所以追求自由民主的人们应该停止使用这个具有欺骗性的砖制狼奶语言体系词汇――“社会主义”。而改用自由民主语言体系词汇“官僚主义”，击中其本质，以此类推：用“官僚主义社会”取代“社会主义社会”；“官僚主义制度”取代“社会主义制度”。<br />
<br />
<br />
　　其实这种由当官的说了算的官僚主义制度有很多的变种：比如古代汉文明在秦之前的封建贵族制度；秦之后的中/央集权世袭皇帝制度，人类近现代出现的很多没有世袭的，中/央集权制度、独/裁制度、砖制制度等等，这些表面上名称各异的制/度，其实本质都是一样的，都是民权小，官权大的官僚主义制/度，而所谓的“社会主义制度”，其实是众多有些微差异的官僚主义制度中，把官僚主义发展到极限的制度，是最最最官僚的官僚主义制度，是把民权缩的最小，把官权增的最大的制度。做的露骨、做的绝的时候是：你家里自己养只鸡下蛋自己吃都不准（要割“资本主义”尾巴，实为剥夺民众的经济投资权），甚至自己家里生火做饭都不准（“公共”大食堂，实为“官办”大食堂）；做的不那么露骨但效果同样的时候是：通过官府权力垄断你生产生活的各个经济活动环节，从执照（官府的投资许可证）到暂住证（官府的地域择业许可证），处处都要官府的官僚们允许才行，从而实现了对官府的官僚对民众的奴役和剥削。看清这些说法不一、形形色色的制度，认识到它们的“民权小，官权大”的官僚主义本质之后，如果有人说基国是砖制制度、独/裁制度、现代封建制度、寡头政治制度、禅让的皇帝制度、官本位制度、权力社会...等等也就不奇怪了，因为它们的本质都是一样的,都是“民权小，官权大”的社会制/度。<br />
<br />
<br />
　　所以用这些称呼都可以，区别不大，但是追求自由民主的人们决不要去使用哪个骗人的砖制狼奶语言体系词汇“社会主义制度”。<br />
<br />
<br />
　　学到这里，现在大家做做练习，说说下面这句话：“社会主义好，资本主义坏，社会主义是公有制，资本主义是私有制，公有制对人民有利，私有制对人民不利，人民拥护社会主义制度，人民反对资本主义制度，”怎么样，感觉到没有，是不是觉得朗朗上口，顺利成章，这就是砖制狼奶语言体系表达起来天然就对砖制有利，对自由民主不利。所以大家追求自由民主的人们如果用砖制狼奶语言体系词汇反驳起来也非常费劲，如果改为使用我们已经学习的自由民主语言体系词汇，这就很简单了，上面这句话就变成了“官僚主义好，自由民主坏，官僚主义是官有制，自由民主是民有制，官有制对人民有利，民有制对人民不利，人民拥护官僚主/义制/度，人民反对自由民主制/度”对比一下，大家自然就明白使用两种语言体系所起的作用。<br />
