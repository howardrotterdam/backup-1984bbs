&nbsp;&nbsp;<br />
　　14、政府和教会分离<br />
<br />
<br />
　　下一个砖制狼奶语言体系词汇是“政/教分离”。这有一个砖制陷阱词汇，因为它扩大了语义，起着帮助砖制统治者镇/压信教民众的反抗的作用，这个词在自由民主制/度中的原意是――“政府和教/会分离”，根本没有政治和宗教分离的意思，而把这个词叫做“政/教分离”，就是诱使人去上当，让人们把它误解为“政治和宗教分离”。从而使砖制官府打击宗教自由，打击宗教人士和各种信/仰团/体变得正当起来。<br />
<br />
<br />
