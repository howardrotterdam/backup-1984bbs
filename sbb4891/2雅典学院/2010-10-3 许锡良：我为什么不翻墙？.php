																											<div id="postmessage_853302" class="t_msgfont">我为什么不翻墙？<br />
<br />
许锡良<br />
有一次年轻的朋友提供翻墙软件给我，要我学会翻墙，软件发给我很久了，我也没有尝试着用一用。朋友感觉奇怪，问为什么不尝试一下。我说，我对那些境外关于国内的信息没有多少兴趣。因为，一切对于我来说都是透明的。我更喜欢于作基础性的研究。<br />
<br />
 一个社会最重要的是价值观与思维方式。这是一个社会制度的基础。而制度的设置，又自然会导致社会的不同事件。国外无非报道某某又怎样了，宫廷内幕又是怎样怎样。我不是法官，也不是纪检人员，对那些在制度情境下无法避免的事件没有多少深究的兴趣。我只对事件发生的机制感兴趣。如果在这个问题上不去挖根究源，那么，即使抨击了事件与人物本身，又能够解决什么问题呢？从五四运动，到民国，再到共和，这种批评抨击还会少吗？可是解决了什么问题呢？赶走了一个个坏的，却来了一个个更坏的。因为，过去那些批评大多只是针对什么“四大家族”的垄断与腐败。却只把矛盾对准某人与某党。就如一块腐肉上生了蛆，却只有焦点对准蛆，而轻松放过腐肉一样。<br />
<br />
 因此中国社会问题，应该建立在更深的层次的追问上。这种追问要从历史根源与文化因素作细致的清理与分析。甚至要把根源追究到各自的文化源头那里，去作一个对照。因此，分析各自的经典名著与重要的历史人物就很有必要。因为文化是会传承的，历史是会沿袭的，榜样的力量是无穷的。代代是会相传的。历史上每一代人以及每一件重大的类似历史事件几乎都可以在另一个时代找到。这就是文化的传承性，国民性格的相似性。那是因为文化基因的遗传性决定的。除非一个民族经受重大的外来文化的冲击与洗礼，否则变异是不容易发生的。<br />
<br />
 中国目前问题多多是肯定的。这一点是共识。但是至于产生问题的原因与性质，却见仁见智，各有不同的看法。有人认为这是传统文化积重难返的结果，因此开放是唯一出路；也有人认为这是中国传统文化断层的结果。只有回归传统文化，才能够救中国。产生这样的分歧其实并不难理解。因为这是对文化的理解不同，也对历史的理解不同，造成的。<br />
<br />
用回归传统文化来解救中国的人，其实就是文化保守主义。文化的核心部分其实就是价值观，依次是制度。因此，文化其实是包括制度的。说韩国与朝鲜是同一种文化，既有一定的道理，又不完全正确。韩国如果就价值观与制度设置来说，所实行的文化已经是美国式的。而朝鲜目前实施的文化价值观与制度安排，其实还是中国 “文革”时的，或者苏联式的。至于两国语言文字相同、服饰相近，饮食相同，这仍然只是表浅层次上的。当年的东德与西德其实也不是同一种文化。就价值观与制度来说就是如此。中国大陆与台湾目前其实也是不同的核心文化在起作用。<br />
<br />
 对中国传统寄很大希望的人，我估计一般是不读历史，而只读圣人经典的。他们只是从圣人字句上去理解中国的历史面貌。事实上并不需要通读中国的二十五史，只需要读读司马迁的《史记》，再读读司马光的《资治通鉴》，你就会感觉中国传统文化在中国历史上使人毛骨悚然残酷事实。那是根本没有什么温馨可言的。因此走回头路是没有出路的。有时从情感上可能会留恋一下，但是从理性上看应该走出新路子来。<br />
<br />
 中西方文化之路确实是大不相同的。就文化源头来说，西方的二希文化，即古希腊与古希伯莱，他们的代表文物，一是苏格拉底，一是耶稣，他们都是为真理而死的殉难者。这代表了他们在文化源头就有一种求真务实的态度，为追求真善美，可以不惜个人的身家性命。在真理面前与人的生命面前，既没有高低贵贱之别，也没有上下等级之分。这种文化中自然天然地就带有一种悲剧意识，这也是他们比较能够吸收历史教训，主动变革，以追求更真、更美、更善的文化基因。<br />
<br />
 但是，中国不同。中国主流文化的源头儒家的代表人物，从孔子、孟子到荀子，再到程子与朱子，凡是号称为“子”级别的代表人物，竟然没有一人是为道而殉难的。这并不代表他们那个时代就有多么太平，多么盛世，而是他们在关键时刻都选择了趋利避害,竟然没有一个人勇敢地站出来以命护道。这种文化源头的开始人物，就成为中国后来的万世师表。在这样的万世师表的表率作用下，中国文化及受中国文化熏陶下成长起来的中国人精神与性格，就缺乏了一点血性。因此，中华民族的历史，算起来竟然就是一部被侵略、被侮辱的历史。而且每都把侵略胜利者看成是自己的正史。元朝是蒙古人入侵，清朝是满族人入侵。无论谁入侵，只要你入侵赢了，你就是我的爷来，就是我的娘。至今中国人还是把元朝侵略扩张的版图看成是中国人的骄傲。也把满清入关看成是中国人的又一次胜利。由于中国文化缺乏柏拉图式的本质观念，也缺乏康德式的绝对理念，因此，中国人怎么都可以。唯一不可以的是你不能够输。凡是你有办法赢的人与事，都是伟大的、光荣的与正确的，因为历史是胜利者写的，胜利者是不应该受到责备的。还因为，中国的圣人提倡道，却并不准备以死捍道，相反还以道作为工具去追名逐利，博得一官半职，这也就为几千年来的中国人树立了一个坚固的榜样。<br />
<br />
&nbsp;&nbsp;中国的历史到现在都没有一个拿出手的样板。回归中国传统以拯救中国的说服力实在是差强人意的。一切的现实因素，其实都可以在历史文化中找到根据。但是，这并不是说现在的人就可以摆脱责任。因为，现在的人也是将来的人看来的历史人物与历史事件。每一代人都将是历史人物，每一代人都必然受死人的牵连，同时也必将影响一下代人。发动“文革”这一代确实是坏，但是，这一代坏人，也是受上一代人教育的结果。如此，一直可以推到孔子这个圣人。<br />
<br />
 中国人，就是中国文化。你的文化是否优秀，根本不用看别的什么经典，只要看这种文化蕴育出来的人是怎样的素质，怎样的眼界与情怀就足够了。把中国文化输入世界，不如让中国人走向世界。让孔子来影响中国，代为中国的代言人，不如让现在的中国人高素质地走出国门。我就是这样看待一个国家的文化的。你的国民素质高，社会清廉高效，人权保障高，社会和谐稳定，你的文化就是优质文化。否则，不用多说什么，就是劣质文化。活着的当代人，就是文化的最好诠释。因此，中国目前最有文化的地区不是北京、上海，更不是圣人家乡，而是香港、台湾这样的地方。因为他们的人相对素质要高，社会更为清正廉洁高效。哪里能够有效解决腐败、暴力问题，哪里就是有文化的。因为在野蛮人那里，暴力是最常见的现象。只有摆脱腐败与暴力，才是有文化的标志。<br />
<br />
&nbsp;&nbsp;一个“中国特色”，让中国自绝于世界之外。因此，现在如何让中国回到世界的怀抱，回归世界的主流价值观，这是当下最为急迫的事情。世界可以通约的是人性。在人家那里有效，而在你这里无效，并不说明那个无效的东西没有价值，而是说明你这里的“中国特色”需要变革。目前，中国特色，使中国的政治体制无法与人家对话，中国教育不能够融入世界评价标准，得不到人家的承认，甚至是医学也不能够得到人家的承认。自顾自地玩一套游戏规则，却又把这个国家玩得如此糟糕，同时也把自己玩得如此孤独。<br />
<br />
 其实，要了解中国并不难，如果学会了思考，学会了观察，掌握了价值观与制度差异，那么，一个社会会发生什么，而不会发生什么，这不是很清楚的吗？<br />
<br />
&nbsp; &nbsp;许多人以为理工农之类的学问可以没有“中国特色”，但是，人文社会科学类一定是要讲“中国特色”的，其实无论什么学问，在思维与价值层面都不会有什么特色。普世价值不会因国情不同而不同。逻辑理性也不会因为民族不同而不同。权力腐败规律是世界相通的。在这个权力腐败规律面前没有一个社会，一个民族可以例外。没有任何途径可以绕过这个规律。在这个规律面前没有什么特殊材料做的东西。<br />
<br />
&nbsp;&nbsp;国人对时论时评常常很关注，这当然也是需要的，但是就事论事的评论不会解决实质性的问题。唯有学会思考，有大脑与心眼去，重新审视核心价值层面的东西，才能够抓住问题的关键与要害。因此，我不翻墙，做猎奇的孩子，而是去寻找那些能够经住时间陶洗之后仍然能够留下来的东西。<br />
