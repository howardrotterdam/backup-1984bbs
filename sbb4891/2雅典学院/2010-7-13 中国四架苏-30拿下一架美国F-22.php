<br />
&nbsp;&nbsp;<br />
作者：涛走云飘 于 2010-07-12 19:07:19&nbsp; &nbsp;<br />
&nbsp;&nbsp;<br />
美军F-22&quot;猛禽&quot;战机进驻东南亚引起了人们的广泛关注，针对美军戴旭上校早前发表的《第27中队入侵》文章，有中国军事分析人士对此发表了不同的看法，分析称，尽管F-22隐形战机实力超群，但始终不是一架&quot;无敌&quot;的战机。分析最后得出结论：四架俄制苏-30完全有能力拿下一架F-22。<br />
<br />
<br />
《国际展望》文章称，戴旭上校在文中针对F-22战机全面的优越性能，说道&quot;在可预料的时期内，甚至世界上在研的战斗机中，也没有一种可以与F-22A全面性能相媲美，不对称的态势还在急剧倾斜着&quot;。然而分析称，纵观二战后美苏间的战机竞争，往往是美国先推出一款新式战机，苏联则在几年后推出可与之抗衡的战斗机。苏联解体后俄罗斯国力损失巨大，已无力全面与美国抗衡，但俄罗斯毕竟继承了原苏联主要的科技力量和大部分工业设施，在战斗机、轰炸机、强击机等主力机型方面尚能与美国抗衡。<br />
<br />
<br />
分析还说道，尽管目前的确没有一个国家的战机能比F-22先进，但他国新研制的战机没有必要与F-22的全面性能相媲美。只要能在关键性能上取得优势，就有取胜的希望。此外，对于戴文上校&quot;在相当长的时间内，不仅在东亚、亚洲甚至整个世界，都看不到平衡隐形空军的军事技术力量&quot;的言论，分析表示，现阶段平衡隐形空军的军事力量主要表现为两个层面：一是靠先进的战机和其他支援飞机；二是凭借先进的防空兵器，在地面将其击落。分析还举例表明：美国的首款隐形战斗轰炸机F-117当时被称为&quot;不会被发现和击落&quot;，但其在科索沃战场上被南斯拉夫人民军用老式苏制防空导弹击落的事实给美国泼了一盆冷水。<br />
<br />
<br />
所以说，东方国家暂时没有可与F-22对决的战斗机，那么研制出能发现和击落隐形飞机的防空兵器已近在咫尺甚至已是事实。比如俄罗斯目前普遍装备并大量出口的S-300系统和即将服役的S-400系统。对于戴文上校文章中说道的作战方式：&quot;开战之前由F-22A和B-2首先清除敌方的弹道导弹，以保证美军不受威胁......&quot;分析表示，即使公路机动的和露天部署的非机动弹道导弹被摧毁，然而，隐蔽并采取加固措施的洞库中的、发射井中的、地下隧道机动的、伪装成普通列车作铁路机动的弹道导弹却无法保证摧毁。如此一来，美军的大举进攻还是有风险的。对于中国方面破解F-22战机的方法，分析称：中国有数量极为庞大的老式战机。如果将歼-6改装为无人机，将老式歼-7升级。由它们佯动、吸引、消耗敌人，具体说来便是分散扰乱F-22的注意力，消耗其空对空导弹、燃油和资讯处理能力。然后先进战机升空与敌决战。将很有可能遏制隐形战机的攻势甚至瓦解其攻势。另外，对于目前被称之为最有实力与美军F-22对抗的俄制苏-30战机，分析称：苏-30在与F-22的一对一对抗中或许不是F-22的对手，但在多对一的情况下，苏-30还是很有把握取胜的。早前有资料显示，F-22战机的战力为美军F-15C&quot;鹰&quot;的四倍，而在印美空军2005年的联合军演中，苏-30完全有能力战胜F-15，如此说来，四架苏-30即有可能战胜一架F-22。<br />
<br />
<br />
至于经济效应，F-22每架售价高达3.5亿美元左右，而苏-30每架约3500万美元，只相当于F-22的1/6，这样说来，单是空军的较量中，四架苏-30拿下一架F-22还是比较划算的。</div>

							
							
							
							
