二氧化碳的主要排放源燃煤发电，也就是火电，只要限制了火电，就可以极大程度地减少二氧化碳排放，而火电在排放二氧化碳的同时，也同样排出二氧化硫、烟尘等，这些是主要的环境污染物质，所以，限制了火电的同时，也相应地减少了二氧化硫等主要大气污染物的排放。<br />
<br />
限制火电就是采用清洁能源、可再生能源，比如风电、核能、生物质能、海洋能、地热能、太阳能等等，水电当然也是一种可再生能源，但因为建设过程以及建成后对环境的影响，所以存在着很大的争议。有一种说法，以人类现有的技术，用可再生能源是完全可以代替化石（别看成石化）能源的。</div>

							
							
							
							
