																											<div id="postmessage_370291" class="t_msgfont">全球变暖和“碳排放”背后的阴谋<br />
<br />
William Engdahl 威廉恩道尔<br />
<br />
由温室气体排放导致的“全球变暖”，10年来被西方主流媒体说成是影响到人类生存的第一位“头等大事”；可是把人类活动导致的二氧化碳排放说成全球变暖的第一因素是不科学的，在这种大张旗鼓的宣传中，极可能包含着打压中国和其他后发国家长期发展的巨大谋略。<br />
<br />
“全球变暖”假设中隐藏着重大的根本性科学谬误：<br />
<br />
西方讨论全球气候变化的模型（以极短的150年的数据为基础DD假定是工业化和历史上的碳排放导致气候变化）没有包括太阳活动的影响；其实太阳活动和太阳周期，才是影响地球大气变化的第一位的、决定性的因素，但这又是一个完全不可能预知的变数。海洋中出现的暖水区域DD“艾尔尼诺”现象，用太阳活动变化可以较好地解释；“CO2－大气变暖”派不去这样解释这个现象，只是说“无法解释”。在“全球变暖”的喧嚣中，气候变化的复杂性和已经出现的“变冷”的现象被主流媒体和美欧刻意掩盖；其实在欧洲已经发生了连续两个冷冬，但是这个“数据”没有被加入大气变化的预期模型。<br />
<br />
更加合理的思考是：大气的变化由太阳活动主导，全球很可能在太阳周期中导致了一个“变暖周期”，现在可能已经进入了“冷周期”；而在今年底哥本哈根大会上，将要决定后发国家必须承担和支付莫须有的控制气候变化的成本，此时变暖问题自身已经开始减弱和消失了。后发国家再次被美欧大玩一把（也许要等下一次哥本哈根大会的时候去改变“碳负”规则吧）。<br />
<br />
Co2【二氧化碳】确实有“温室”功能，但是它是大气成分中比重最大的一种成分，工业生产燃烧能源排放的Co2，会在大气中有限度上升，随后一定会逐渐下沉，或者随着大气团的运动而升降分散，最终覆盖在地球的土地表面，为绿色植被提供光合作用原料。另有科学研究证明，全球海洋表面排放的二氧化碳总量，超过人类活动的排放总量。谴责人类二氧化碳排放造成温室效应的理论，不能解释（也不去解释）为何Co2事实上没有升到万米高层空间、形成保温层，去阻挡地面温度向宇宙空间散发，却发生了地面和大气温度的难以解释的变动（大气对流层的厚度是5-8千米）。关于“全球变暖”的700科学家倡议书，事实上只有4个人真正签署了名字，这个事实应该能够说明非常多的问题。<br />
<br />
“碳交易”是掠夺后发国家财富的最新的大阴谋：毫无悬念。<br />
<br />
有评论者指出，碳交易的国际市场发展速度极快，非常可能在几年之内（不超过10年）就在交易的规模上超过巨大无比的国际石油市场；这个前景极有可能是巨大的陷阱，而不是维护全人类持续生存条件的举动。美国芝加哥期货市场2003年出现了“气候期货指数”CCX（背后是华尔街大银行），至今已经交易多年。国际机构如WB和IMF、金融巨头如高盛等抢先规定，后发国家如中国不许自行在企业之间、不许同他国自己进行“碳指标交易”，必须首先得到他们的允许、通过他们指定的公司和机构（在中国由北京、上海、天津三处产权/环境交易所代理），必须用他们的规则和由他们定价，等等。欧洲市场的吨碳价格（20欧元/吨）是中国的两倍以上，已经抢先介入中国碳交易的高盛等公司，可以轻松预期最低100%的回报率。最近有报道（新浪），高盛已经出资400万美元“注入”北京“环交所”进行碳交易，其中的200万被指定用于新疆青海等中国西北内陆区域的“绿碳”计划，植树种草，它们将顺便插足当地经济政治，一旦进入就永远不会离开DD这一笔区区400万美元的举措，应该当作为重大战略行动看待。我国被指定的三大环交所非常乐意从事碳交易，从表面和短期来看，这是天上掉馅饼的好事。但是我们的环交所充其量只是芝加哥期货市场和CCX的三级代理，日后当我们自己的几大环交所开始互相竞争的时候，芝加哥就可以坐收渔利了。<br />
<br />
美国从1997－2003年来，始终拒绝批准“京都议定书”，不仅仅是自私自利，还因为他们深知“底线”：人类活动导致的二氧化碳排放：大气变暖的说法，只是用来哄骗不知情的国家和人民的。美国作为最大排放国，对碳排放的长期后果毫不担心；只待“碳交易市场”成长到足以为美国经济注入活力的规模之时，美国就要批准“京都议定书”，利用碳交易谋利了：现在2009年正当其时。所以今年年底要召开哥本哈根大会了。<br />
<br />
对于后发国家，以致全球，真正的长期威胁，是环境污染和资源耗竭（关于石油生成有另一种理论，认为石油不会很快耗竭）。哥本哈根大会并不准备讨论这两个问题。中国许多地方的环境污染已实现了断子绝孙的水平（如陕西凤翔查明615名儿童血铅超标），我们自己也不去管，反而去跟风“碳交易”。<br />
<br />
低碳在技术的方向上总体是正确的，但是从地缘政治和全球发展谋略的角度看，倡导这个技术的真实目标，是遏制后发国家的工业成长速度――“伪全球化”30年，把中印等国的经济活力调动起来，现在快要构成对美欧工业的威胁了。让我们低碳（后发国家研发低碳技术的能力远不如发达国家），或者把落后的高碳技术送/卖给我们（这是常态），让我们交“碳税”（美国国会今年5-11已经通过了《碳关税法》），就可以使我们永远快不起来、永远追不上去。<br />
<br />
中国在跟着哥本哈根的框架推进低碳技术（完全有必要）的同时，必须思考更加全面和长远的工业发展战略和技术战略。<br />
