													<h2>小白看世界:现阶段的中国不适合推多党执政</h2>
						
						
																											<div id="postmessage_397930" class="t_msgfont">首先说明,我是一个政治小白,没有执政经验,没有真实的资料来源,只有自己的所见所想,想在这里说一下自己的看法和疑惑. <br />
因为听说这里立场中立,冷静思考的牛人很多,希望会有人帮忙释疑,反正大家有砖照拍就是了.<br />
<br />
首先,我觉得刘晓波因言获罪是这个时代的悲哀,在现阶段是比较无奈的.<br />
<br />
第二,我觉得现阶段的中国没有合适的土壤培养发展多党执政的局面.<br />
<br />
第三,我的话题是围绕第二个观点的,,,轻点砸,我也知道这是句废话,但我是指望它来承接上下的,水平有限啊,没办法...<br />
<br />
其实我也不知道怎定义前面说的合适的土壤,脑子里只有一个模糊的概念,应该跟所谓的中产阶级差不多吧.<br />
反正是需要有这么一群人, 他们生活过得去,不怎么会因为现实生活问题怨恨这个社会,懂得冷静思考,对社会现状有着比较清晰和理智的理解,关心国家的发展.<br />
至于需要多大的一群人,我是觉得要大的让国外的势力没可能控制,同时当前政党为了社会的稳定不得不妥协的程度.<br />
当这样的人群取代当前还在为房子车子票子问题挣扎的大众,在社会上占主导地位的时候,他们的共同意愿应该是比较符合国家发展需要的,对政府的影响力也会比较强.<br />
而现在,据说和刘晓波一起签名的才300多个,是可以给政府一点点压力,但是处理起来却不费什么劲,在国内社会上也没牵起多大的波澜,起码是没有一点动荡.<br />
为什么呢?<br />
就因为现在还不是时候,现在大多数人最关心的还是怎么改善生活.<br />
很多年轻人不知道刘晓波这件事,即使知道了,他们也会这样想吧,民主是很重要,但是最重要的还是先帮我把房价搞下来吧,不然我老婆娶不到(因为没房子),,老了嫁不出(因为很多男的没房子),孩子不敢生(因为两个人一起还是买不起房子), 父母照顾不了(还是因为没有房子)...刘晓波是判1年还是11年,就让那些有心思折腾民主的人士去折腾吧,我还得被我的房子我的生活折腾...<br />
现在作为社会主流的出来打工的年轻人大多都是处于这种状态吧,残酷的现实让他们不得不对这样的事件漠然处之,拜金的潮流让他们的注意力一直集中在物质生活的改善上面.<br />
这样的社会团体是不可能在眼花缭乱的政党纷争中保持冷静而理智的思考的,他们很容易被利用作为斗争的工具,最坏的情况,国家四分五裂也是有可能的.<br />
说到当前拜金的潮流,我觉得是必然的,因为国家在集中精力发展经济,而国家的行为对社会大众的行为应该是起导向作用的,用古人的说法,上有所好,下必甚之,不过现在这个上是指政府,下是指百姓而已,道理应该通用.<br />
<br />
我们回顾一下历史,也可以发现类似的现象...<br />
<br />
春秋时期，皇权贵胄都好攒名誉，养N多门客什么的，在那文风盛行，百家争鸣的时代，拜金主义铁定没门，那时候谁敢晒家里钱多，那是要被口水淹死的。 <br />
<br />
战国时期，纷纷战乱，祸及天下，随时都有可能国破家亡，各国基本都尚武求存，那时候人们的生活的最大压力应该怎样才能活下来，而不会是有没有钱，或者自己的钱比别人多或者少多少吧。 <br />
<br />
跳过一些不熟悉的朝代，到了唐宋，虽说重农抑商没那么严重，但刚从诗词的盛行程度就应该可以知道当时人们对精神生活的向往应该是仅在温饱之后的，金钱肯定不会排在第一位，应该在第三，或者更后面（前面可能还有个仕途）。 <br />
<br />
再跳一下，到明朱王朝，商业有所发展，但是金钱依旧是排在第三或者更后面，无他，就因为朝廷提倡孝悌为先，那时候谁敢偷老妈的私房钱去吃喝玩乐，当真可能要被砍头的。不像现在的年轻人，老家伙不给钱去上网，就抢了再痛扁一顿也没人管。 <br />
<br />
看看现在，国家政策这几十年来都是向经济发展倾斜的，应该可以说是一直把和平发展放在第一位的。 <br />
<br />
国家尚且要闷着头赚钱，老百姓怎能不受影响呢。 <br />
<br />
邓老提倡的让一部分人先富起来的政策背后，可能就考虑到让人的本性去驱动其余的人去追逐财富，达到富民强国的目的。 <br />
<br />
开始大家都没有钱，突然某些人因为政策和机遇变得有钱，生活变得富足，其他人看到了就会开始羡慕，向往，或者嫉妒，这都是人的本性。 <br />
然后有些人会发现嫉妒没用，就会有所行动，有样学样，或者另辟新迳，发财致富，，，人的本性就转化成了社会经济发展的动力了。 <br />
建国初期的国人大部分都是贫农，还以此为豪，那时的人们所体现出来的人性只是比较单纯的一点点。发展经济后，人们变得有钱了，受的教育也多了，受到了金钱和物质生活的影响，并随着各种认知的发展，慢慢开始尝试以前想都不敢想的东西，很多因为贫穷落后而从未曾体现的人性开始得到释放，发展... <br />
<br />
改革开发以来政府都是把重心放在经济上面，就是说政策是向经济发展倾斜的，那么当前人们的观念向金钱倾斜就不足为奇了。 <br />
<br />
这个国家的建立本身就是一些有识之士带着一群农民头打下来的，建国后相当一部分农民头和他们的后代成了利益所得者，而现在我们看到的乌烟瘴气主要应该就是这部分人搞出来的，要分阵营的话，他们是属于利益阵营的，他们只服务于自己的利益。他们无所不在，而且通常是打着为国家于人民服务的旗帜。 <br />
<br />
而那些有识之士和他们发展起来的团队(也包括一部分比较正直爱国的农民头啦），则是属于国家阵营的，他们才是真正服务于国家与人民的，他们是国家的中流砥柱，也许我们很难把他们分辨出来，但我们不能忽略这部分人的存在。 <br />
<br />
因为利益阵营的存在，拜金主义的盛行在所难免，国家阵营的人在内困外扰下能把中国发展到今天这种地步已经很难得了。 <br />
<br />
这种情况会延续多久，应该是取决于阵营之间的博弈吧 (之前有跟人讨论过这个,他说我把阵营划分的太简单了,这个我完全同意,其实我只是想把意思表达出来,具体的阵营划分对我来说真是太复杂了)。<br />
<br />
也许等我们这一代人的生活标准达到期望,下一代的期望就是民主吧,这个过程应该比较漫长,但是我还是看好的.<br />
