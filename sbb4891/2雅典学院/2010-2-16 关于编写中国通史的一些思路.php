																											<div id="postmessage_482309" class="t_msgfont">关于编写中国通史的一些思路<br />
刘松萝<br />
<br />
1. 编写一部新的中国通史的必要性<br />
<br />
很长时间以来，我一直想与志同道合的人们一起编写一部中国通史。之所以产生这样的想法，是因为我们目前阅读的通史受政治观点的影响太大，而老前辈编写的通史则不能够反映历史研究后来取得的成果。还有，前辈编写的通史有的可读性不够，有的脉络不够清晰。<br />
<br />
之所以想自己来做，是因为自以为有一些独到而务实的思路。<br />
<br />
这是一个难以胜任甚至难以完成的工作，故而首先把编写思路整理出来。<br />
<br />
2. 肯定和继承前辈的成果<br />
<br />
吕思勉先生的《吕著中国通史》，钱穆先生的《国史大纲》，翦伯赞、范文澜先生编纂的中国通史，都是值得借鉴的。<br />
<br />
张荫麟先生未完成的《中国史纲》，日本人内村湖南先生的《中国史纲要》，在思路上是很有启发性的。我还想说，孙仁宗、楼均信、王正平先生编写的《绘画本世界通史》（浙江少年儿童出版社2000版）具有公允的历史观和气势磅礴的叙事，也值得借鉴。<br />
<br />
3. 不标新立异<br />
<br />
不否定前辈的工作，继承前辈的成果，自然会引出以下的看法：将要编写的通史并不是一部全新的通史，也不宣称应用全新的手法，甚至不追求新手法。<br />
<br />
经验告诉我们，试图构建全新的历史，比如阶级斗争史学，儒法斗争史学以及潜规则史学，结果都是很糟糕的。另外，我也反对应用类似“大历史”一类的概念，因为我们并没有理由证明前辈所写的是“小历史”。<br />
<br />
4. 编写的是“正史”<br />
<br />
将要编写的通史是一部“正史”。<br />
<br />
所谓正史，是说在记述当中，政治生活、政治斗争和统治者的活动仍然占据很大的比重。对于历史事件，将使用中性的语言来描述。如果有评价，也主要依据当时的标准，适当参照现代标准。<br />
<br />
对于统治者的行为，并不刻意批判。对于其中的暴君，对其残暴的一面进行谴责。<br />
<br />
5. 仍然是帝王将相史<br />
<br />
梁启超先生认为，二十四史非史也，二十四姓之家谱而已。鲁迅先生认为，所谓“正史”是为帝王将相作家谱。尽管如此，将要编写的通史仍然要用大多数篇幅介绍帝王将相的活动。原因很简单：在古代社会，帝王将相的作用是不可替代的，我们无法忽视。<br />
<br />
另外，每一个帝王的名字，实际上是时间的刻度。<br />
<br />
6. 保持历史叙述的连续性<br />
<br />
不遗漏每一个皇帝，对于重要的政治家，至少要提到名字。即使是我们认为没有什么价值的时代，也要加以概述。有些部分，可以用小字印出，作为参考。<br />
<br />
7. 对于重要的人物，要提到他的归宿<br />
<br />
对于重要的历史人物，要提到他最终的结局。<br />
<br />
比如了不起的齐桓公，不仅在兄弟相残中得位，而且在凄惨中死去，死后又是诸子夺位。虽然这不能否定齐桓公的伟大，但同时也告诫我们，伟大的业绩不能代替对于权力更替规则的重视。<br />
<br />
再比如对汉朝的很多开国功臣，要提到不少人死于非命，还有不少人的后代被夺去了爵位，甚至被杀。这些记述，不仅有助于我们思考有关制度的建设，还可以给现在从政或者准备从政的人们以参考。<br />
<br />
8. 对有些历史人物，要给予像帝王一样的重视<br />
<br />
大家知道，曹操虽然没有做过皇帝，但在历史记载当中的重要性超过了当时的皇帝。还有，对于西汉的王政君，其影响力从汉成帝开始，一直持续到王莽篡汉，因此必须重视。抓住了这一主线，讲述汉朝走向衰落的过程就会清楚得多。<br />
<br />
9.让历史上的文化人物进入主要场景<br />
<br />
我这样想，假如要削弱帝王将相史的色彩，就应该让历史上的文化人物进入主要场景，而不是在介绍完一个朝代的重大事件之后，让他们在“文化”部分出现。<br />
<br />
比如在谈到唐玄宗时，就应该让李白和杜甫出现。<br />
0. 描写朝代兴起与终结的场景<br />
<br />
频繁的改朝换代，是中国历史的一个特点。朝代更替的时刻极具戏剧性和传奇性，在通史中不可以平平淡淡地成为过场。<br />
<br />
描写朝代兴起与终结的场景，描写其中的细节，不但可以增加通史的可读性，而且可以给人们留下更多的启示。关于细节，有哪些朝代更替完全是暴力的，哪些是通过“禅让”来实现的。<br />
<br />
对于朝代灭亡时的忠臣，应该有所记述。近几十年来，国人不断批判和讥讽忠臣，但忠诚与坚守仍然是值得赞扬的。<br />
11. 叙述重大历史事件时，附注外国的历史事件<br />
<br />
司马迁的《史记》，翦伯赞先生编写的通史以及柏杨先生的《中国人史纲》都这样做了。<br />
<br />
12. 对于像佛教传入中国和道教创教等事件，要像朝代建立一样重视<br />
<br />
对于像佛教传入中国和道教创教等事件，对于中国人的信仰、思想和生活有着重大的影响，要给予像朝代建立一样的重视。<br />
<br />
以往通史对宗教的记载不够突出，以至于电视剧常常让“阴曹地府”和“玉皇大帝”出现在东汉以前的故事里面。<br />
<br />
13. 对于境外精神和物质文明的输入，要加以记述<br />
<br />
华夏民族曾经向境外输出文明，我们一直引以为荣。同时我们必须认识到，境外很多民族也向中原提供了精神和物质的文明。<br />
<br />
在通史中，应该讲述诸如佛教、琵琶、胡琴、唢呐，以及石榴、黄瓜、番茄等等从境外进入中原的事实。此类记述也应该进入主要场景，甚至可以与重大历史事件并列。有了这种记述，也许人们在写小说和拍电视剧的时候就可以避免在西汉出现辣椒和红薯了。<br />
<br />
在讲述华夏民族在文明上的进步的同时，也应该承认匈奴和突厥的锻铁技术曾经高于中原。<br />
<br />
有些变化，比如中原从北方民族那里引进“胡床”，应该被视为重大的历史事件。从此，汉民族的坐姿与以前不一样了，这是生活方式的巨大变化。<br />
<br />
<br />
<br />
14.记述其他民族的活动，记述民族迁徙的历程<br />
<br />
我们知道，在中国的历史中，作为农业民族的华夏民族与北方游牧民族的对峙，占据着很大的比重。甚至很多次改朝换代，就是由于游牧民族的入侵造成的。<br />
<br />
在中国古代的历史著作中，就有介绍周边民族状况的传统。在通史中，也应该介绍同时代存在的其他民族的重大事件和他们以往的经历。与汉民族不同的是，有些民族的起源和演进不是十分清晰。不过，大致的脉络还是有的。在以往有些通史中，喜欢把汉民族的历史写得言之凿凿，其他民族却是来历不清楚，甚至是像突然出现的。<br />
<br />
对于曾经活跃在北方，以后不见于史籍的民族，也要交代一下其下落。值得记载的是，在我们现在的国土上出现过的民族大迁徙，应该作为重大事件加以记载。<br />
<br />
比如，北匈奴西迁，以及猜测中的北匈奴到达欧洲，触发了灭亡罗马的进程。比如，月氏民族在被匈奴击败之后迁徙，称为大月氏，建立过包括中亚和印度北部的贵霜帝国。没有大规模迁徙的那一部分月氏民族被称为小月氏，以后溶入其他民族之中。有些之姓汉人，自称为小月氏的后代。再比如，一部分突厥人西迁，留下一个“突厥走廊”一直到达小亚细亚。今天的土耳其人，就是古代突厥中的勒与小亚细亚希腊移民混合而成的。<br />
<br />
还要指出的是，在历史上，有些汉人在北方民族的压迫之下南迁，同化了一些南方的少数民族。这也是一种民族迁徙。<br />
<br />
以往，为了政治的需要，不但模糊其他民族的起源，不承认其他民族在其神话中显示的民族起源，甚至不承认中国正史中对突厥和回纥起源于匈奴或者是“匈奴别种”的记载。更加积极的历史学家，甚至否认回纥与突厥的同一性。有一个很强的偏向，就是只能说匈奴与东胡接近，而不是与突厥接近。另外，对李唐王室，只能说有鲜卑血缘，不可以提到突厥。还有，对土耳其人，一定要说是西突厥西迁的结果。<br />
<br />
在现实的国际政治上，一些土耳其人确实有着干预中国事务倾向，应该加以警惕。同时，土耳其人面对中国人的时候会说“我们过去是邻居”。作为大民族，国人也要有这种豪爽。<br />
15. 不以通俗为标榜，为追求<br />
<br />
试图编写一部让大众能够较为流畅地阅读的通史，但不以不以通俗为标榜，为追求。<br />
<br />
第一，叙述口语化，但主要还是使用专业语言。全部使用口语，将会使历史庸俗化。<br />
<br />
第二，不能为了吸引读者而把通史变成评书。叙述历史，是为了让今人受到启迪，而不是讲述有关吃亏占便宜的道理。<br />
<br />
16. 追求脉络的清晰<br />
<br />
如果对前辈们提一点意见，那就是有些前辈在叙述中逻辑不够清晰，还有些认不能够找出要点来。当然，前辈的前辈其实不是这样的。<br />
<br />
柏杨先生的《中国人史纲》在描述五胡十六国历史的时候，不但生动，而且能够把那么复杂的过程写得脉络清晰，值得称道。</div>

							
							
							
							
