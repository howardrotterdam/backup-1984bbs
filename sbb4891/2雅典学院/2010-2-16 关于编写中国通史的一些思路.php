																											<div id="postmessage_482463" class="t_msgfont">19. 关于疑古与信古<br />
<br />
我不是疑古派，也不是信古派。对于古代传说，当然不能全都相信。不过在没有过硬的材料之前，我们只能参考传说。应该说，上古的人，远比我们诚实。<br />
<br />
关于夏朝的历史，目前只能认为是传说。是商以前的历史，还是周族对本民族历史的回顾，还需要存疑。目前，确定二里头就是夏都或者夏朝大城市，完全无法接受。<br />
<br />
关于黄帝传说，要存有一些疑问。存在时间靠前，记载却靠后。不排除有出处，但无法相信黄帝一定是尧舜的起源。不过，作为民族信仰，不反对黄帝传说。<br />
<br />
关于尧舜禅让，《尚书》与《竹书纪年》有不同的记载。现在，前卫的人们倾向于采信后者。我认为，假如有人说正史会有所隐瞒，那么不太主流的历史也可能有过度的怀疑。不能说禅让就是美化，其他民族连选举都能够实现。<br />
<br />
作为我，则要在通史中说明，《尚书》的记载是尧舜禅让，而《竹书纪年》的记载是舜囚尧。<br />
<br />
同样，关于西周的共和行政，我要说明，《春秋》的记载是周公和召公共同主政，而《竹书纪年》的记载是共国的国君，共伯主政。<br />
<br />
20. 对民间传说的重视&nbsp; &nbsp;<br />
<br />
历史学者会这样地藐视一个人：他的历史，全是从《三国演义》中来的。这就告诉我们，在通史中，必须对《三国演义》、《隋唐演义》、《杨家将》以及《岳飞传》涉及的人和事做出说明和解释。<br />
<br />
回避了上述历史文学作品，并不能提高社会的历史素养。就是在当前，不论是公众还是专家教授，有很多人的历史知识其实就来自演义小说，甚至来自金庸先生的武侠小说以及《戏说乾隆》等等。<br />
<br />
在通史中，有必要挑出几个问题来说明，比如告诉读者王朗不是诸葛亮气死的。<br />
<br />
当然，我们还应该感谢演义小说，公众从那里知道了历史人物。<br />
<br />
关于民间传说，我想这样处理：讲述春秋吴越之争，越王勾践卧薪尝胆，终于灭亡吴国时要提到西施，再说明西施是后世在历史中增加的。至于是记载了口传的历史还是纯属民间传说，不做结论。有学者直接去掉西施，不是很好的方法。</div>

							
							
							
							
