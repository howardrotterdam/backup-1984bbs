																											<div id="postmessage_482648" class="t_msgfont">21. 关于历史记述中的文学因素<br />
<br />
假如说某人所编写的历史像文学作品，通常是一种贬义的评价。对司马迁和他的《史记》，就常有上述的评价。不过，假如我们看看从古到今西方人所写的历史，就会知道历史绝不应该是枯燥乏味的。<br />
<br />
学究式的史书在中国历史上就存在，在清朝，因文字狱和学术上的考据爱好而强化。从上个世纪50年代开始，苏联式的教科书让历史更加乏味。<br />
<br />
我在《面对历史》中说过，历史同现实一样，都是生命之树结出的果实。一部枯燥乏味的历史，同样是不真实的。<br />
<br />
对于文学家撰写的历史，应该允许在有限的几个地方妙笔生花，或者发一发感慨。比如，柏杨先生在《中国人史纲》中，对引出无数祸端的美女夏姬发出过感叹：“我们假设夏姬第一次结婚时十六岁，儿子夏征舒十六岁时杀死妫平国。那么纪元前五九九年，她已三十二岁。到纪元前五八四年跟巫臣结婚时，至少已四十八岁。真是不平凡的女性，不仅仅驻颜有术而已。可惜处在那个时代，她只能被她所不能控制的命运摆布。”比如，柏杨通过北宋史节在金国的观感，展现了金军的无比强悍。再比如，柏杨对北宋背盟联金灭辽时，辽国使臣的哀叹。<br />
<br />
文学是人学。通过文学，我们可以更加了解人的本性，感悟命运的无常与无情。一部史书是否生动，不在于里面有多少俏皮话，而在于作者能否选取并且写好历史中有价值和有震撼力的人物、事件和场面。<br />
<br />
更重要的是，不了解人性，对命运没有感悟的人，很难对材料做出合理的取舍。关于这一点，后面还要提及。</div>

							
							
							
							
