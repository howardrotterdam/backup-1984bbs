																											<div id="postmessage_482150" class="t_msgfont">关于编写中国通史的一些思路<br />
刘松萝<br />
<br />
1. 编写一部新的中国通史的必要性<br />
<br />
很长时间以来，我一直想与志同道合的人们一起编写一部中国通史。之所以产生这样的想法，是因为我们目前阅读的通史受政治观点的影响太大，而老前辈编写的通史则不能够反映历史研究后来取得的成果。还有，前辈编写的通史有的可读性不够，有的脉络不够清晰。<br />
<br />
之所以想自己来做，是因为自以为有一些独到而务实的思路。<br />
<br />
这是一个难以胜任甚至难以完成的工作，故而首先把编写思路整理出来。<br />
<br />
2. 肯定和继承前辈的成果<br />
<br />
吕思勉先生的《吕著中国通史》，钱穆先生的《国史大纲》，翦伯赞、范文澜先生编纂的中国通史，都是值得借鉴的。<br />
<br />
张荫麟先生未完成的《中国史纲》，日本人内村湖南先生的《中国史纲要》，在思路上是很有启发性的。我还想说，孙仁宗、楼均信、王正平先生编写的《绘画本世界通史》（浙江少年儿童出版社2000版）具有公允的历史观和气势磅礴的叙事，也值得借鉴。<br />
<br />
3. 不标新立异<br />
<br />
不否定前辈的工作，继承前辈的成果，自然会引出以下的看法：将要编写的通史并不是一部全新的通史，也不宣称应用全新的手法，甚至不追求新手法。<br />
<br />
经验告诉我们，试图构建全新的历史，比如阶级斗争史学，儒法斗争史学以及潜规则史学，结果都是很糟糕的。另外，我也反对应用类似“大历史”一类的概念，因为我们并没有理由证明前辈所写的是“小历史”。<br />
<br />
4. 编写的是“正史”<br />
<br />
将要编写的通史是一部“正史”。<br />
<br />
所谓正史，是说在记述当中，政治生活、政治斗争和统治者的活动仍然占据很大的比重。对于历史事件，将使用中性的语言来描述。如果有评价，也主要依据当时的标准，适当参照现代标准。<br />
<br />
对于统治者的行为，并不刻意批判。对于其中的暴君，对其残暴的一面进行谴责。<br />
<br />
5. 仍然是帝王将相史<br />
<br />
梁启超先生认为，二十四史非史也，二十四姓之家谱而已。鲁迅先生认为，所谓“正史”是为帝王将相作家谱。尽管如此，将要编写的通史仍然要用大多数篇幅介绍帝王将相的活动。原因很简单：在古代社会，帝王将相的作用是不可替代的，我们无法忽视。<br />
<br />
另外，每一个帝王的名字，实际上是时间的刻度。</div>

							
							
							
							
