													<h2>韩寒：而你们在怕什么</h2>
						
						
																											<div id="postmessage_567613" class="t_msgfont">今天，在一些论坛上，我看见了福建马尾审判三个网友，说真的，我的确完全不知道是怎么回事，于是，我就开始搜索，我先上了谷歌香港，结果就和我搜索胡萝卜和李子一样，页面再也无法打开，于是我上了百度，我只知道有三个网友，因为涉嫌诽谤，被抓了起来。那么他究竟诽谤谁了呢，诽谤了当地公安，我当时就想，这三个人完蛋了，诽谤了当地公安，再由当地法院审理，这审判结果岂他们不是都已经在当地饭店里谈好了。<br />
 <br />
本着公正的态度，我继续搜索这三个网友究竟是为什么诽谤了政府，但是线索到这里就断了，我先用了百度知道功能，百度表示不知道，当然，知道了也不会告诉你，于是，我使用了新浪爱问功能，结果爱问不问。最后，我大致知道了故事的内容，这其实是一个很典型的故事，为了防止出现文章过敏的情况，我一概隐去了人物姓名。<br />
 <br />
主人公突然死了，家属怀疑是被轮奸致死，提出尸检，尸检的结果是主人公自顾自病死，并非强奸致死。家属怀疑警方包庇罪犯，提出还要尸检，但是相关部门并不配合。家属的情绪很不稳定。三位维权人士听闻此事，以死者是被强奸至死的观点，的将此事件做成了视频和文章，转发到国内外论坛。当地公安召开新闻发布会，强调死者是自然病死，随后，参与制作视频发帖等人当地公安机关逮捕，其中主要负责的三人，二审被判有期徒刑一年到两年。<br />
 <br />
事情大致上就是这样的，就案情本身，关键是死者到底是怎么死的，这个我不知道，我也没有证据，所以无法站在维权者或者政府的任何一方说事。政府认为，只要他们宣布了，这就叫证据，维权者认为，只要他们调查了，这也是证据。这件事情我并不了解，在其他众多的维权事件中，政府一定全错么，不一定，维权者一定全对么，也不一定。但是为什么政府永远表现出全错的态势呢？<br />
 <br />
其实很多事情其实都是当地政府自己弄大的。如果他是真的自身突发疾病死亡，那么便让有公信力的地方来尸检，说服家属便是。很多网友说，政府急需成立廉政公署，来树立公信力，我认为廉政公署没有用，香港很少发生腐败事件，其实并不是拥有了廉政公署这四个字，而是因为廉政公署是独立的这三个字。我认为，大陆现在的国情是不适合成立一个独立的类似廉政公署的机构的，如果一成立然后来真的，那几乎所有公务员及其亲属都嗖一下不见了。但是，大陆最最急需成立的一个部门乃是独立的“尸检部”，这个尸检部必须拥有向廉政公署一样的独立性和公信力，必要的时候做到电视直播尸检。仔细回想中国近几年发生的公众事件，有多少是因为尸检而生。尸检部是维护社会稳定的重要部门，因为现在的尸检结果，无论是真的假的，老百姓都不相信。排除这件事情，虽然我认为，很可能不少的尸检结果是正确的，但老百姓的怀疑也不是错误的。一个喜欢先定性再定罪的政府，其绝招也很容易被老百姓学去，所以说，我们要原谅老百姓动不动就认为自己的亲人是被人害死，而罪犯是被政府包庇了，尸检是被政府操纵了。因为在这个社会里，你不讲证据，那我也不讲证据，你不透明，我就猜测，我一猜测，你就说我诽谤，我再追究，你就说这是国家机密，我一闹大，你就……你就……你就反而省事了，什么事情都不用做了，自然会有相关部门通知新闻部门这事情不准报道。但是，这一切埋下的都是仇恨的种子。<br />
 <br />
所以说，对于地方政府，这事情其实一开始就很好解决，绝对公正的尸检，如果真的是病死，说服家属，如果真的是被杀的，捉拿凶手。退一步讲，这个事情完全可以放到台面上，公开的去说清楚，大家拿各自的证据来说事，但官方是从来不屑于这么做的，他们认为这样降低了他们的威信。而且我们的官方永远是一开口就置自己于不利，你从来看不到他们坦诚的用我们人类正常交流的语言说话的时刻，永远用没有人性的官腔去对抗老百姓的哭诉。听音乐的朋友都知道，唱腔是多么的重要，一个你讨厌的唱腔，唱什么歌都是错的。<br />
 <br />
现在，那位女孩子是怎么死的已经不重要了，更重要的是那三位维权者的判决。至于诽谤罪，看来是非判不可了。类似的罪名不能从法律上去诠释，而是要从人情世故上去找结果。因为面子对于政府是很重要的，人家都关了你这么长时间了，尤其是因为这个理由，现在把你放出来，你法院是能得到喝彩，但是人家公安以后怎么混，都在一个县城里，抬头不见低头见，办公室不见桑拿见的，这见面了还怎么相处。其实这么多年，大家都误会了人民法院的意思，人民法院并不是指属于人民的并为人民服务的法院，而是指只负责解决人民与人民之间的矛盾的法院。<br />
这三位替他人维权的网友，被判了一年到两年以后，很多网友认为，这件事情代表了互联网的黑暗时期将要到来，代表了维权人士将要遭到打击报复，代表了网络的监督将要受到取缔和法办，代表了言论自由的彻底消失，我认为其实这些都不是，小小的一个县城的公检法，你不能把人家想的那么深邃，其实这件事情只代表了一个意义，传达了一个讯息，那就是――让你见识见识我的厉害。<br />
 <br />
是的，让你见识见识我的厉害。我们见识了，我们都很害怕，但是我们也不知道，你们都在害怕些什么。</div>

							
							
							
							
