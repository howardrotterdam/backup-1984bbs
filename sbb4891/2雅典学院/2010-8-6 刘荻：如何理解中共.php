																											<div id="postmessage_771056" class="t_msgfont">2010-08-05<br />
中国的民间反对派中，有主张和平演进的，有主张暴力革命的，有希望中共内部改良的。除此之外还有一些人，他们的观点是：因为中共万恶，所以不可能内部改良，同时因为中共万恶，而且中共想做什么都能做得到，所以中共也不可能容忍体制外的和平演进和暴力革命做法，因此中国人民只有等死一条路可走。这些人把中共当成一种不受客观规律制约、不受环境影响、全知全能而且邪恶的超自然力量，在这种超自然力量的淫威下，个人无论怎么努力都是徒劳无功的，只有等死。当然，持上述观点的人中，也有一部分人指望另一种超自然力量来拯救自己，而且有些人认为自己能够通过某种方法与这种超自然力量沟通，以此来拯救世界。<br />
<br />
我们当然不准备等死，也不能把自由的希望寄托在超自然力量上。首先，中共并不是超自然的邪恶力量。中共和所有事物一样，要受到客观规律的制约和客观条件的限制，不可能为所欲为。如果中共想做什么都能做到的话，那共产主义早就实现了。<br />
<br />
其次，中共也不是由一个灵魂在指挥的统一整体，一举一动都是由统一的灵魂精心策划的，反映整体的最佳利益。中共和其他组织一样，是由人组成的，而且这些人和其他人没有什么两样，都是追求各自目标的理性的人。我们可以把中共的行为理解为无数个人在一定的规则和条件下彼此互动和搏弈的结果。理解中共的最佳模型不是撒旦模型，而是“群魔乱舞”模型。<br />
<br />
有些人根据亚当&#8226;斯密的理论，认为既然组成中共的个人都是理性的，那么每个人追求个人利益最大化一定能导致整体利益最大化，因此虽然中共的行为是个人之间互动和搏弈的结果，但这一结果一定还是符合中共的最佳利益的。这种观点是对亚当&#8226;斯密理论的误解。每个人追求个人利益最大化能够导致整体利益最大化，这只是在一定的制度环境下如此，绝非在任何情况下都是如此；在另一些制度环境下，每个人追求个人利益最大化可能会损害整体的利益。根据奥尔森的集体行动理论，一个群体越大，就越不可能为了整体的利益而采取行动；要使一个大群体为了整体的利益采取行动，就必须为其成员创造个人的激励机制，而创造这种激励机制需要企业家才能。<br />
<br />
总之，我们没有理由认为中共党员一定会为了中共的利益采取行动，也没有理由认为中共的一举一动一定是精心策划的符合其最佳利益的行为。这种看法高估了中共的能力。持有上述看法的人会认为，如果中共没有阻止米奇尼克访华，那一定是经过精心考虑，认为米奇尼克访华对自己有利无害，等等。但实际情况可能是，负责此事的具体官员没把米奇尼克当回事，或者觉得没必要承担不许他入境的责任，等等。作为中国持不同政见者，我们时常能够接触到对中共当局持批评立场的外国人，包括外交官、记者和人权组织官员等等，绝大多数情况下这种会面不会受到阻止。中共当局或许是没有能力得知所有有关信息并且加以阻止，或许是觉得阻止这种会面会带来麻烦，因此一般情况下没有必要阻止，但是如果有人说所有这些会面都是中共当局认为对自己有利，因此“许可”的，那么我们可以肯定，这个人并不了解中国的现实。<br />
<br />
如果我们不把中共当成超自然的邪恶力量，而是将其当成由和我们一样不断变化的个人组成的组织，这些人会追逐自己的目标，会对根据环境调整自己的应对措施，那么我们就不会说我们的对手是“最自私、最没人类意识、最无现代政治理念、最无能，因而最喜欢用最凶残、最简单的办法解决社会问题的一群”，而是会像米奇尼克一样，认为中共内部的实用主义者虽然没有理由追求民主，但出于实用主义考虑却有理由与民主反对派达成妥协，因此有可能成为民主反对派的伙伴。那么我们要做的就不是痛骂对手，而是努力创造条件，迫使对手和我们妥协而不是进行镇压。用莫之许老师的话来说，就是：“有没有党内改革派不重要，有没有党外反对派才重要，或者说，有了党外反对派，自然就有党内改革派，党外反对派是党内改革派出现的前提，而不是相反。”</div>

							
							
							
							
