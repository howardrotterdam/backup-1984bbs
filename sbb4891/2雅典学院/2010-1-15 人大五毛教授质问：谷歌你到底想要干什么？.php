 来源：人民网 2010年01月15日<br />
外交部回应Google退出事件 称网络很开放<br />
 　　谷歌是个互联网公司，名气很大，块头更大，其商业模式在世界范围内获得成功。但它现在的架势让人看不懂了。它向中国提出了政治要求，而这种事情通常是大的商业公司极力避免的。<br />
<br />
　　两天来，谷歌威胁要退出中国市场，一些西方媒体发出赞扬，并迅速把这一事件变成了批判中国的好机会。美国国务卿希拉里站出来指责中国，国会议员发出更加刺耳的声音。在这一事件中，谷歌处于风暴的中心，而且它的角色明显在变。<br />
<br />
　　起先，谷歌抱怨的原因是服务器受到“攻击”，后来，竟演变成要让中国取消“网络审查”。从互联网安全的一个普遍问题，跳到了要要求中国改变法律。这一跳真是让中国人吃惊。<br />
<br />
　　谷歌究竟是在做商业，还是在搞政治？如果是商业，那就应当限定在商业范畴。有纠纷不要紧，可以通过商业的途径来解决，但要是想通过商业行为来影响中国政治大局和社会改革进程，那可就完全改变了事件的性质。<br />
<br />
　　当前，中国互联网上的管理有一些争议，但怎么改，要充分尊重中国的现状，也要以中国社会的承受力为基本判断标准。<br />
<br />
　　中国社会目前已经相当开放，互联网的成长不断激励着它，今后的中国只能更加开放。但是，任何开放都是有度的，而且开放要有一个进程，不可能一蹴而就。中国的门该开多大，路该怎么铺，要中国人自己来设计、安排，外界怎么说，只能作为参考。一个互联网公司，即便拥有最先进的技术，最丰厚的资金，就想左右这一进程，都是狂妄的。<br />
<br />
　　谷歌在中国经营三年多，但似乎对中国的历史与现实了解的程度还很低。中国是主权国家，不是19世纪时的租界，外国企业没有治外法权。在中国经商，想游离于中国法律之外，是对21世纪中国的误读。西方社会、跨国公司对中国建设性的批评和善意的主张，是有助于中国发展的。但如果背后隐藏政治企图，并想用一种威胁的方式来迫使中国就范，根本行不通。<br />
<br />
　　谷歌要中国取消网络审查制度，其结果只能像美国“Mashable”网站的一篇文章所说：“如果有人幻想中国会改变政策，让人无限制地接触反动、淫秽图片等内容，那纯粹是无稽之谈。”<br />
<br />
　　中国这些年一直在西方某些人的鞭笞和叫骂声中成长，谷歌威胁引来更多责难没什么可怕的。倒是这一次西方媒体对谷歌威胁退出一边倒地叫好，让中国民众不能不多一层警惕之心。<br />
<br />
　　英国《卫报》预测，如果谷歌摊牌，互联网世界将分裂。中国当然不希望这种分裂，但也不怕任何麻烦，更不能被这种预言所挟持。在谷歌威胁面前，中国社会必须要团结。<br />
<br />
　　许多中国民众都愿意使用谷歌的搜索业务。没有谷歌在中国的业务，可能会暂时影响到一些中国人的网络生活。所以，我们欢迎谷歌留下，但留下来的谷歌必须要做出调整，也要适应中国的法律。如果谷歌坚持要退出，没什么了不起。退出，是自绝于中国市场。<br />
<br />
　　（作者是中国人民大学国际关系学院副院长。） (来源：人民网-《环球时报》)</div>

							
							
							
							
