													<h2>转</h2>
						
						
																											<div id="postmessage_671590" class="t_msgfont">　“右派”，你们为什么不忏悔<br />
　　　　　　　　　　　　　　　・司鹏程・<br />
<br />
<br />
　　１９５７年是平淡一年。中共建政已逾八年，历经剿匪反霸、土地改革、镇反肃反等运动的洗礼，地、富、反、坏等诸多阶级敌人早已“千百万人头落地”。１９５７年又是特殊的一年，习惯冷漠与鲜血的中国知识分子，此时要为红色政权奏响“百花齐放”的凯歌。<br />
<br />
　　１９５７年春夏之交的那场政治运动是毛一时兴之所至还是当局刻意设计的引蛇出洞，殊难考证。但毋庸讳言，“右派”们无不是抱着治病救人、帮党整风的初衷，对中共提出建言的。孰料高瞻远瞩的毛主席受到蒙蔽，将精忠报党者打作反党反社会主义，这不能不说是历史的误会。<br />
<br />
<br />
１、反右――一次“文雅”的政治运动<br />
<br />
<br />
　　资料显示：从１９５７年６月到１９５８年夏末，全国共清理出５５２，８７７名“右派”。另据１９８０年６月１１日中共统战部《关于爱国人士中的右派复查问题的请示报告》，反右运动实划“右派”４９万余人。<br />
<br />
　　应该说，和中共的历次运动相较，反右这场“政治战线和思想战线上的社会主义革命”无论在深度、广度和持续时间上都称得上“温文尔雅”。<br />
<br />
　　运动中遭受密集批判的是所谓“三大反动理论”，即章伯钧的“政治设计院”、储安平的“党天下”和罗隆基的“平反委员会”。章不满意“国务院开会常是拿出成品要我们表示意见”，认为国家大事应“事先经过国务院的部长们（章伯钧本人即荣赝中共交通部长）多方面地讨论”；储抱怨“毛主席倡议和党外人士组织联合政府。可是后来政府改组，原来中央人民政府的几个非党副主席，他们的椅子都搬到人大常委会去了”；罗则肯定中共“肃反有很大的成绩，但是也发生了副作用，使人不敢讲话”，他敦促中共以“国士”对待知识分子，“解决党和非党隔膜”。这些言论被视作“对党怀有刻骨仇恨”，章、罗、储也沦为“疯狂向党进攻”的一群。<br />
<br />
　　值得注意的是：章、罗两人后来多次在公开场合否认自己说过“政治设计院”和“平反委员会”类的话语。因此前述语言是否出自中共的归纳或假想，姑且存疑。即便如此，运动中遭受批判的“右派”言论俱无出这“三大理论”之“右”。<br />
<br />
　　为什么“右派”选择１９５７年发声？回顾历史，我们不难发现：经过中共八年的屠戮和清洗，红色政权的各色敌人此时已被清理干净，天下抵定。而俨然以新政权功臣自居的“精英”们出于分一杯羹的心理，试图向以往的盟友讨要轮流坐庄的权利。例如储安平就诘问中共：“是不是非党人士中没有一个人可以坐副总理的交椅？”谁知愿望落空，他们不免如失宠怨妇般抱怨三两声。但令人始料未及的是，“独坐池塘如虎踞”的毛绝不容他人分享权力，早已打定主意要去除自己 “身上的虱子和害虫”。<br />
<br />
　　众所周知，中共长期以来将“右派”视作人民的一部分。即便此刻出于“保持党的健康肌体”需要，将其从人民行列划为敌对阵营，对“右派”的处理也仍然有别于地主、富农、反革命和坏分子等“四类分子”。<br />
<br />
　　以反右高峰时期，中共中央转发各地，以作“右派”处理“样板”的《对一部分右派分子处理的初步意见》为例。该文件共涉及９６名“右派”，其中绝大多数被撤职、行政降级（一般降４、５级），另有监督劳动、开除公职、送劳动教养、免于处分等。兹举数人如下：<br />
<br />
　　章伯钧，撤去交通部长一职，工资从行政３级（月薪５１７元）降为７级（副部长级，月薪３２２元）。保留全国政协委员职务。原住房不予变动。配轿车和司机，用车由“吉姆”降为“别克”。<br />
<br />
　　罗隆基，撤去森林工业部长一职，工资从行政４级（月薪４６０元）降为９级（司局级，月薪２５３元）。原住房不予变动。无轿车和专用司机。保留服务员、炊事员及警卫员。<br />
<br />
　　章乃器，撤去粮食部长一职，工资从行政４级（月薪４６０元）降为１０级（月薪２１８.５元）。保留服务员、炊事员、警卫员。１９６３年，改为每月发１５０元生活费。后经章反映，增至每月２００元，但房租、水电费、电话费等自理。<br />
<br />
　　这就是大“右派”们的际遇，虽然“悲惨”但仍然让普通民众仰慕。<br />
<br />
　　对待各色中小“右派”们，当局则执行１９５８年１月２９日的《关于在国家薪给人员和高等学校学生中的右派分子处理原则的规定》。该文件划定了对 “右派”的六类处理办法：劳动教养或开除公职；撤职送农村或农场监督劳动；留用察看，并降低待遇；撤销原职，分配待遇较低的工作；降职降级降薪；免予处分。据此，罪行严重的“右派”将可能失去“铁饭碗”和公费医疗，加入普通民众的行列。<br />
<br />
　　应该说，全国各地大小“右派”的落魄遭遇于其个人是场悲剧，但和早已进入地狱的诸多“牛鬼蛇神”相比，实在是天上人间。要知道，那些地、富、反、坏分子从来就是当局“杀、关、管”的对象，他们及他们的家属经历了共产政权下的各种歧视和迫害并欠缺最基本的话语权，直到今天。<br />
<br />
<br />
２、“右派”是谁？<br />
<br />
<br />
　　中共对待阶级敌人从不手软，它总是检讨自己将“匪首恶霸特务”杀得太少、镇压不及时；杀人过后的总结也不外乎以后要“打得更稳更狠”、 “避免宽大无边”等。独有“右派”备受青睐，得到宽大的待遇；“右派”问题始终是“人民内部矛盾”或“按人民内部矛盾处理”。原因何在？<br />
<br />
　　１９５８年５月３日，中共宣布反右取得阶段性胜利。其时，当局统计称运动共清查出党员“右派”２７８，９３２人。这个数字颇能说明反右实乃大水冲了龙王庙的历史的真实。<br />
<br />
　　就个案而言，“全国最反动、最猖狂的右派”葛佩琦即是标准的共产党。葛１９３８年加入中共，受命潜入国军。辽沈战时，他任职东北保安司令部，以 “少将督察”、“东北通讯处处长”的身份剌探情报。而被誉为“三大右派毒蛇”之一的章伯钧更是在１９２３年即经朱德介绍加入中共。章的两个弟弟俱是共产党，长弟早亡，次弟被斯大林清洗枪决，都未能尝到共产主义的甜头。章本人曾参加过南昌暴动，也算为中共鞠躬尽瘁。<br />
<br />
　　除了为共产主义献身的“右派”党员外，其他人几乎毫无例外的是中共同路人或支持者。罗隆基、储安平及其所代表的政党在四十年代末的国共内战中，作为中共的坚定盟友出现，被毛夸奖作搞垮国民政府的“第二战场”或“第二条战线”。中共党史这样评述章、罗、储等人：“一是从政治上打败了蒋介石。二是从军事上配合了第一条战线作战。三是从组织上为革命和建设准备了大批骨干。四是配合解放军接管城市。五是直接配合了三大战役的军事斗争。”中共夺取政权后， “论功行赏”，章伯钧、罗隆基荣赝交通部部长和森林工业部部长，储安平也捞了个《光明日报》总编的职务。<br />
<br />
　　除前述两类外，“右派”中还有一类特殊群体，即高等院校中的学生。他们长在红旗下，基本上根红苗健，家庭属“依靠对象”或“团结对象”，在获得组织认可后被选拔进入无产阶级学术殿堂。对这些人，中共文件设定了较一般“右派”更和缓的四种处理办法――“保留学籍，送农村或农场劳动考察；开除学籍，劳动教养；留校察看；免予处分。”（参见中共１９５８年１月２９日《关于在国家薪给人员和高等学校学生中的右派分子处理原则的规定》）毛后来并明确指示： “右派学生首领一般宜留在原地管教，并当‘教员’。”人民大学女学生林希翎即成为“教员”代表。林的罪行在于她不满于“我们现在的社会主义不是真正的社会主义”，主张将“混蛋和投机的党员要清洗出去”、“老实而不起作用的，要动员他们退党；只保留真正的共产主义者”。林被“开除学籍，留校监督劳动”。五十年后，旅居海外的林希翎谈起反右，仍是一脸气愤，“我是一个真正的左派，他们凭什么、凭什么把我打作右派！”<br />
<br />
　　毋庸讳言，“右派”从没有背叛中共，从没有否定共产极权制度――无论是公开还是私下，无论事前还是事后。（至于在６０年代后狂乱的政治氛围中走投无路，最终转向反对共产专制者，例如林昭，则不属本文讨论之列。）在１９５７年的那场运动中，所有的“右派”，都声嘶力竭地辩解自己拥护党，竭尽全力地表述自己热爱社会主义的真诚。罗隆基被打为“右派”后，委屈地说：“即使把我骨头烧成灰，也找不到反党阴谋。”其言凿凿，其情切切，实在令人啼笑皆非。<br />
<br />
　　有鉴于此，１９５８年１月２８日，毛在最高国务会议上强调：“对右派的处理要比较宽大。要留一条路让他们走，使他们有可能回到人民的队伍里来。”“右派”们也确实没有辜负伟大领袖的期望，他们为重回革命阵营，为争取入团入党，对自己昔时的亲朋故旧反戈一击，做了淋漓尽致的表演。应该说，从１９５８年开始，“右派”们就纷纷“起义”，弃暗投明，回到了人民的怀抱。<br />
<br />
　　反右运动结束未久，中共即开始检讨这场自家人打自家人的运动。从１９５９年到１９６４年，中共先后五批摘掉３０余万人的“右派”帽子。１９７８年９月１７日中共下发《贯彻中央关于全部摘掉右派分子帽子决定的实施方案》，对“右派”予以“彻底的完全的平反”；即“实事求是地予以改正……恢复政治名誉”，原是党团员的并“恢复党籍、撤销开除团籍处分”。中共声称：“反右错误要坚决加以改正，目的是为着团结一致向前看，同心同德搞四化。”（参见１９８０年６月１１日中共中央批转的《关于爱国人士中的右派复查问题的请示报告》）截至１９８０年５月８日，曾经被划为“右派”的５５万人几乎全部获得 “平反”，仅有个别人“只摘帽子，维持右派原案，不予改正”，其中包括中共中央认定的章伯钧、罗隆基、彭文应、储安平、陈仁炳五人以及由各地处理的９０余人。<br />
<br />
　　显而易见，“改正”是一种姿态，它意味着当局正式承认反右是一场错误的运动，是大水冲了龙王庙；这种表态对“从来不下罪己诏”的中共来讲可算极为罕见。在１９８１年中共１１届６中全会上，“右派”又被进一步追认为“爱国人士和党内干部”。<br />
<br />
<br />
３、“新”中国右派的众生相<br />
<br />
<br />
　　中国文人与生俱来就有“御用”的情结。入翰林院，为皇帝效命，是光宗耀祖的事。但中国儒家也讲“士可杀而不可辱”，这是说知识分子要有做人的尊严，理念和原则是应该用生命去维护的东西。这种操守即便在历代封建王朝也为士林清流所推崇，远有“义不食周粟”的伯夷，近有自蹈昆明湖的王国维。<br />
<br />
　　但君看今日树头花，不是去年枝上朵。１９４９年以降，知识分子一语不再具有任何道德价值。当１９５７年之时，共产中国治下的“右派”脸谱尤为繁多，更令人叹为观止。<br />
<br />
　　他们是精于算计的一群。他们早认识到极权制度的本性。例如储安平４０年代中期就曾与人言：“我们现在争取自由，在国民党统治下，这个自由还是一个多少的问题；假如共产党执政了，这个自由就变成了一个有无的问题。……我不相信在共产党的统治下，人民能获得思想及言论等等基本自由，能实现真正的民主。”（参见储安平《共产党与民主自由》一文，载１９４５年１２月１日的重庆《客观》周刊）然而，荣任新王朝开国部长、总理职务的“最后的贵族”的迷梦，却驱使他们昧着良心说话，协助中共颠覆了国民政府。<br />
<br />
　　他们是助纣为虐的一群，党的人血馒头向来少不了他们一份。<br />
<br />
　　远者不论，从１９４９年到１９５７年在他们那所谓的新生革命政权下丧生的千百万人是谁之罪？仅以１９５０年的“镇反”为例，中共官方的统计是清剿各色“政治土匪”２，０００，０００人，另有特务份子６００，０００人、反动党团骨干６００，０００人。中国各地的５５万“右派”，此时扮演了何种角色？从镇反到公私合营、从土地改革到批斗胡适，多少屠杀，多少批判，难道他们从来都是受害者？反右前的那一次次政治运动，究竟是谁在上、在下、在左、在右煽风点火并推波助澜？莫非所有的罪恶和屠戮都是毛一人所为？<br />
<br />
　　中国“右派”，你们有几个不曾为“镇压反革命”欢呼雀跃？你们有几个不曾为中共“社会主义建设的新成就”摇旗呐喊？你们又有几人选择独立正义的立场，哪怕只是保持简单的沉默？<br />
<br />
　　有学者记录说：“百分之九十被扣上右派帽子的人，在反右初起时都曾经跟着批判过首批被定为右派的人。为求自保去斗别人，他们以为这样便可做教鸡儆猴里的那只猴，宰鸡时不妨去帮着戳一刀。谁知他们注定进不了猴的队伍，反而成了儆猴的鸡。”<br />
<br />
　　历史是如此惊人的相似。“在德国，起初他们追杀犹太人，我没有出声，因为我不是犹太人。然后他们来对付天主教徒，我也没有出声，因为我是新教徒。后来他们追杀工会会员，我不是工会会员，我不说话。最后，他们奔我而来，再也没有人站起来为我说话了。”<br />
<br />
　　历史又是如此惊人的不相似。中国“右派”没有选择“不说话”的沉默，他们选择了“向人民投降”。１９５７年，从夏天到秋天到冬天的日子里，被划作“右派”和与未被划为“右派”的知识分子，比以往更加卖力地向党表现着自己的忠诚。怀着邀宠的希冀，这些教授名流们痛哭流涕、横七竖八地相互揭发、攻讦，出卖家人亲友，检举揭发自己的同类，践踏别人的同时也被别人践踏……。<br />
<br />
　　这就是反右！它远没有“右派”们所描述的那样惊天动地。“右派”，也从来不是他们吹嘘的那般清白高大。<br />
<br />
<br />
４、“右派”，你们有何资格索赔？<br />
<br />
<br />
　　平淡中走过５０度春秋，历史也早已习惯了冷漠与忘却。<br />
<br />
　　２００７年，坊间突然流传万千“右派”上人民共和国主席书，上美利坚合众国总统书。这些陈情书林林种种，或悲悲切切或义正词严。上书者来自五湖四海，文字各有苍夷，主旨却出奇一致，要求“党和政府给予经济补偿”。<br />
<br />
　　“反右是中国历史上冤者最多、株连最广、历时最长、手段最毒、胜过秦始皇焚书坑儒千百倍的大冤案。”“中国死于一九五七。”“右派不得赔偿，中国没有民主。”<br />
<br />
　　阅罢诸般宣言，直让人觉得：“右派”们为理想和正义遭遇了亘古奇冤，因为他们，方有今日伟大共产国的繁荣娼盛。当然，“右派”们也不忘指出：没有反右，就不会有大跃进和文化大革命，他们就可为社会主义建设作出更多更大的贡献，中国人民的日子会比今天甜蜜上千万倍。<br />
<br />
　　“老右”们为祖国和人民忍辱负重五十年，如今他们需要钞票，要行使“索赔”的权利。一提“索赔”，说话者嗓门就升了许多，仿佛人格也就此清醒高大起来。<br />
<br />
　　然而“右派”们忘记了：中国历史不是在１９５７方被改写。当“开国大典”之时，毛昂首高呼“从此站起来了”的时刻，还没有被划为“右派”的知识分子早已跪将下去。时间在他们向毛敬献“我们永远跟您走”锦旗的那一刻，已经被定格了。<br />
<br />
　　毋庸讳言，中国知识分子在反右运动前是为虎作伥，反右后他们诚惶诚恐地匍匐在毛的脚下，努力为独裁者张目并论证其政权政策的合法性。即便是当年在“最高国务会议”上被毛用流氓手段羞辱作“伪装得最巧妙，把自己描写成了不起的天下第一美人，用笔杆子杀人，反动透顶的杀人犯”的大“右派”梁漱溟，在毛死后也真心忏悔说：“由于我的狂妄自大、目中无人，全不顾毛主席作为领袖人物的威信，当众与他顶撞，促使他在气头上说了若干过火的话。如果说当时意气用事、言语失控，那么也是有我的顶撞在先，才有毛主席对我的批判在后。”<br />
<br />
　　悲乎？中国“右派”从没有什么正义和道德，他们的良心早已死尽灭绝。在极权体制被推向巅峰的过程中，“右派”们兴奋地扮演着共产暴政吹鼓手和广告牌的角色。中华大地万物凋敝、尸骸遍地，而中共非常拙陋的愚民把戏却能肆行无阻、传之久远，这批御用帮闲者的丰功伟绩实不可抹杀。<br />
<br />
　　多年后，当毛早已走入坟墓多时，丧尽良知的同一票人也浑然忘了羞耻，力图将自己打扮成受难的贞女节妇，仿佛当日所有的罪恶都与自己无涉。不仅如此，他们还不忘向党表达自己的忠心。所谓“共产党像是母亲，母亲有时会错打了孩子，但是孩子决不记恨母亲”。显然，只要有金钱的补偿和职位的提升，“右派”是从来不惜将自己卖个好价钱的。颇得此中三味的朱熔基、王蒙也因此攫升为人民国的总理、部长。<br />
<br />
　　而今，“右派”更摆出向中共乞讨的姿态。好个“索赔”，寥寥两字，拧起来是标榜自己清白无辜的牌坊，放下去是向当权者赖狗乞怜般的叫唤，实在是进可攻、退可守的利器。但你们可曾想过：自己应如何偿还中国民众，如何向你们协助建立的独裁铁幕下中国８０，０００，０００死难冤魂赎罪？<br />
<br />
　　平反，平反，平个鸟反！你们有什么资格借助“平反”为杀人者附上正义的标签？中国社会不需要你们这种奴才对主子的专制合法性的认同，相反，它需要的是对罪恶的清算和彻底的历史反省。<br />
<br />
　　古语云“为虎作伥”。伥即伥鬼，自己被虎吃，又帮虎去吃别人。老虎被人捕捉后，还可以听见伥鬼在那里哀伤地哭号。《七修类稿》记载说：“伥鬼死的时候没看见自己的尸体，所以不知自己怎么死的，被虎吃了反而被虎役使，好不容易老虎死了，又悲伤痛哭，这是多么痴愚啊！难道不会内疚吗？”伥鬼与今日的 “右派”何其相似？他们怀念着作威作福的最后的贵族的日子，他们不会去反省自己的过错；反而希望借机依附新的权贵，为自己捞得利益――这不正是虎伥的所为吗？<br />
<br />
　　黄钟毁弃，瓦釜雷鸣；谗士高张，贤士无名。既有鸡鸣狗盗之国，自然盛行妓院勾栏之“右派”――这些一直以“第二种忠诚”效忠中共的知识人――他们在各色政治运动中早已磨砺得良知丧尽。试看今日之所谓学者精英、教授作家，哪个没有在反右中有过精彩的表现，又有几人没有痛斥过资产阶级自由化和法轮功。“说出真相和揭露谎言”的知识界的责任从来同这些御用和半御用的精英们无涉。<br />
<br />
　　嗟夫！共产党之于中国，在于杀尽了中国人的骨气廉耻。<br />
<br />
　　１９５７年早已终结，但中国社会的集体失语和失忆远未结束；经历反右前后如此众多惨痛历史事件的国人，也迄今未有真实的思考和回顾。<br />
<br />
　　但是历史不会忘却，它将永远铭刻１９５７――这是一块猩红色的耻辱柱，上面遗留着中共的印记，也记录下了中国知识分子最丑陋的一幕。<br />
<br />
<br />
