													<h2>南周虚拟访谈章鱼帝保罗：“我并不担心有人会揭发我造假”</h2>
						
						
																											<div id="postmessage_741926" class="t_msgfont">作者: 郭力 <br />
2010-07-15 00:25:12<br />
 来源:南方周末 <br />
■虚拟访谈 <br />
<br />
章鱼帝保罗很忙，几百家媒体要采访他，以至于它要专门雇用一家公关公司来应付。可是无论记者问什么，公关公司永远只有两句话，要么“保罗很累了，需要多休息”，要么“无可奉告”。<br />
保罗最终还是跟本报记者手舞足蹈地聊了20分钟。他其实很淡定，手舞足蹈是因为他说话本报记者听不懂。当然他手舞足蹈本报记者也看不懂，所以它的回答都是本报记者猜的。<br />
<br />
据说，中国楼价是难住章鱼帝的少数几个预测之一。 （CFP/图）<br />
南方周末：见到活着的您真高兴。有人说，在预测下届世界杯冠军时，您面前出现了中国国旗，于是您自杀了。<br />
保罗：谣言止于智者。中国是个伟大的国家，所取得的成就和进步有目共睹。而且你们地大物博，又特别喜欢吃烤鱿鱼，我爱这个国家！<br />
南方周末：说到世界杯，您能预测一下中国足球的前景吗？<br />
保罗：下一个问题，谢谢。 <br />
<br />
南方周末：好吧，很多中国人都托我问问您，中国的股市半年内能翻回3000点吗？<br />
保罗：下一个问题。<br />
南方周末：那中国的房价……？<br />
保罗：好吧，既然你坚持。或许我说涨是大家都满意的结果吧！不过，据我所知，房价应当是遵循市场规律的，供不应求就涨价，供过于求就跌，为什么你会想我猜房价呢？<br />
南方周末：作为神，保罗，您会有很大压力吗？<br />
保罗：这个神，不是我自己想当的，是全世界球迷选我当的。我不能辜负全世界球迷的期望，对不对？至于压力也不会，毕竟我鱼到晚年，无所谓了。而且我从来没有骗过人，我并不担心会有人来揭发我造假。 <br />
<br />
南方周末：您最喜欢的颜色是？很多您的粉丝都想知道。 <br />
<br />
保罗：很多人都说我是喜欢红色才选择了西班牙，还说因为虾都是红色的，那岂不是说谁管饭我就跟谁走？你们以为全世界吃的虾都是红色的，其实你见过煮熟的红壳虾在海里游吗？我吃的虾都是青壳的。<br />
不过，要说我最喜欢的颜色，还是大海的深蓝，那是自由的颜色。 <br />
<br />
南方周末：听上去您经常做哲学的思考。 <br />
<br />
保罗：是啊，你们国家的一个什么子说过嘛，“你又不是鱼，怎么知道鱼的快乐呢”。我一不输出恐怖主义，二不输出意识形态，为什么总有些外国人想要煮了我呢？ <br />
<br />
南方周末：请您给我们预测，2012年人类会毁灭吗？<br />
保罗：前些日子似乎有一个什么总统也来问我这个问题。我反问他，如果会毁灭，他能不能也带我上那个诺亚方舟，他说“一定啊”，因为还要我帮他预测他能不能连任下届总统。我说，你的老百姓都死光啦，你还想着选总统？他就没话说了。我也是替你们着急啊，太简单！太天真！<br />
南方周末：能跟我们讲讲您的感情生活吗？大家都总是看到您一个人在镜头前。<br />
保罗：可是你们那么多狗仔，24小时直播我的一举一动，当明星容易吗？当神就没有隐私了吗？你们还不停地拍我的裸照…… 借这个机会我要告诫一些媒体，新闻自由是法律允许范围内的自由，自由不是没有限度的。<br />
南方周末：有人托我问您，如果把您煮了，是清蒸好，还是红烧好？<br />
保罗：你是哪家媒体的？<br />
南方周末：其实您已经取得了历史性的成就，谈谈您的感想吧？<br />
保罗：先感谢我的祖国大不列颠，她的海水养育了我，我永远是祖国人民的章鱼。另外要感谢父母！是他们用自己的生命，换来我的生命。<br />
南方周末：据说，还有新加坡的鹦鹉、非洲的猩猩等等动物，都在预测世界杯，但是只有您100%猜对了。对那些您昔日的竞争对手，您想说些什么？<br />
保罗：我老了，但是他们还年轻，还有很多可以猜嘛：下一轮经济危机什么时候来，气温明年升几度，LadyGaGa什么时候结婚，博尔特能突破百米5秒吗，还有很多问题困扰着你们人类，不是吗？我们什么时候能登上彩虹？什么时候能摒弃战火？为什么你有一所房子面朝大海却被强拆？这个世界会好吗？<br />
那么，我能问你一个问题吗？ <br />
<br />
南方周末：我太荣幸了！ <br />
<br />
保罗：我就想问问你，你们中国人的第一个问题，为什么不是股市就是楼市？你们还能想点儿别的吗？ <br />
<br />
南方周末：……<br />
