一、“虚拟万里”　　 <br />
　 　 <br />
互联网“老同志谈话”洋洋洒洒，乍读起来它似有退休老干部口气，有些话语还把作者和万里曾有的经历有意连接。然而到了该文最后一大段，“托言他人”制造了一个“虚拟万里”的手法露馅了，那段话是：　　 <br />
“那一年我到广东视察，就有一些当事人向我聊起1976年10月6日的一些事情，印证了我以前的一些道听途说。这一事件的内幕，我们的老百姓在去年的一份南方报纸上看到。在处理“四人帮”过程中，党的秘密情报机关起到了特殊作用，难以说明、又不可缺少的作用，老帅要利用自己的亲属与情报机关作沟通打招呼。这一事实被隐瞒了整整32年。谁承担这种隐瞒的责任？一个执政党的领导人运用亲属、情报机关、军队的力量来解决党内纠纷，这种“下不为例”的事情，真的下不为例了吗？周总理在建国之初就告诫过李克农同志，情报机关不要卷入党内斗争，他的告诫防止了我们党内的那些不规范行为了吗？60年了，执政党即使不方便向老百姓说清楚，至少也应该先向几千万党内同志讲清楚，在赞扬那些为国家进入新局面作出了特殊贡献的人的同时，也应该明确地否定他们所使用的手段，这才符合基本的政治伦理。现在，还没有听到这种否定。一个执政党执政了60年还不注意这一点，要执政能力有什么用？没有政治伦理为基础的执政能力，会变成什么样的能力，我想，大家会认真思考这个问题的。”　　 <br />
实际情况是：万里过去五年根本没到过广东。近年来他住院多次，甚至病危，何及出巡？其二，《南方周末》2008年底“纪念改革开放30周年”约请叶剑英侄子叶选基（叶道英之子）发表长文。“虚拟万里”说自己是在那之后又到广东，这不是乱编吗？文中提到叶剑英通过其二子叶选宁（时任总政联络部长，该部负责对台湾工作）与其他人联系。实际上，这种联系方式当年盛行，因为文革的风气造成只相信可靠的家人的这种心态。胡耀邦，万里本人，邓立群等无一例外。比方说，胡耀邦儿子胡德平就窜来窜去，即使到了八十年代，他还代表“中央很负责的同志”与党内不同意见的高层沟通、疏导。万里的儿子万老四在北京不也是这样做的吗？其儿媳妇还是号称“中南海王熙凤”。　　 <br />
叶剑英并没有动用总政联络部的某个副部长或一个警卫班做任何事。叶剑英知道，自己虽然被陈锡联取代军委日常工作，但叶的“军委副主席”头衔还没被取消。如果亲自出马，难免被外面看成“军事政变”。所以“一生谨慎”（毛泽东语）的叶剑英认为由政治局多数解决少数的方式为佳。他找王震，王震说：汪东兴我还可以说上话。叶：这是个关键人物，你一定要和他保持联系。王震又找陈云，陈也认为“四人帮”要解决，但他起初考虑，学苏联共产党当年赫鲁晓夫开中央全会整倒马林科夫、布尔加宁，绕开政治局委员的表决。然而陈云在认真研究中央“十大”中委会名单后，感到没有把握取得过半数人的支持。叶剑英又约陈到西山一谈，叶把毛泽东批评“四人帮”的记录稿给了陈看，陈说：这场斗争不可避免，但解决方式要合乎党内程序，不是只顾眼前，要顾虑党的传统，要合法。在历史上提出这个事情，不给后人留下坏影响。1976年9月21日，华国峰请李先念到小礼堂看电影，两人借故在厕所“不期而遇”，华委托李找叶剑英，征求如何解决。两人为防窃听，只用纸笔交流。概而论之，解决“四人帮”叶剑英起了重大作用。叶剑英作了部署，但发起动手是华国峰，他指示汪东兴只动用了警卫部队。这不是秘密情报机关动用的手段！华国峰是党中央主席，汪东兴听命于他，师出有名，顺理成章。是开会政治解决问题，而不是某个军事将领出马当一哥老大。　　 <br />
“虚拟万里”歪曲事实，断言党的秘密情报机关起了特殊作用，何其荒谬！当年谁也是以这类理由，这样攻击中国扯下“四人帮”？“北极熊”苏联！台湾蒋经国！　　 <br />
“虚拟万里”借用陈云“下不为例”一语否定清除“四人帮”之举，认为那样做不合“政治伦理”。那么，让江青当党主席，王洪文当国家主席折腾中国下去，就符合“政治伦理”？！客观讲，真实万里是坚决反对“四人帮”，也一直在和党内老同志联系的。证据之一是：1975年4月天安门事件后，造反派出身的副总理孙健，默许铁道部副部长郭鲁纠合一帮人去整部长万里。证据之二是：解决“四人帮”后的几天，执政者采取内部“打招呼”的方式，在政治局已缺少四人的情况下，吸收了王震等人参加政治局会议。王到会一看，大发脾气：“为什么不让万里同志列席这么重要的会议？”　　 <br />
通过分析，就可以看出“虚拟万里”的文章漏洞很多，且不合事实，完全没有可信度。分辨一篇文章真伪，只要发现一段是虚拟的，而且失实虚假的，就可以将整篇文章视为失实虚假，不辨自明。　　 <br />
那么，“虚拟万里”真正持刀者为何人？相信很快会有关部门会循迹查出。我们坚决支持将其投入“秦城”，因为“托信他人”也是某种形式的犯罪。实际上，“虚拟万里”只不过汇总“炎黄春秋”为首的党内右派种种言论、香港杂志那些流言蜚语而已。若认真阅读那些杂志，你会发现什么“执政的合法性”等种种提法早就出现过。另外，持刀人发现5月份赵某人录音讲话成书后不少人好奇，决定仿而效之也来个“虚拟万里”录音讲话以惑众。你看，什么鲍彤、李大同都跳出来断定那是万里，宁可“赌错”真正的作者。为什么？只因为很多话是来自他们自己的。　　 <br />
　 　 <br />
二、真实万里　　 <br />
　 　 <br />
万里目前健康状况　　 <br />
万里（1916年生，山东东平人），现年93岁。过去几年间，万里患病曾多次住进医院，三次有“病危通知书”报告中共中央政治局常委会。　　 <br />
之后，万里家人找到民间医生并予以土方治疗，与中央保健部门发生争执，万里四子万季飞的妻子（号称中南海王熙凤）代表家人坚持土法治疗。结果病情好转，数月前已从医院搬回中南海内住所。“假万里”文章称：“那一年我到广东视察”“我们的老百姓在去年的一份南方报纸看到，在处理四人帮过程中，党的秘密情报机关起了特殊作用。”时间就不对嘛！万里这五年都没到过广东，何来视察？何来关心“去年的一份南方报纸？”万里本人家属都断然否认“一位老同志的谈话”出自万里或家人之手，对“假万里”深表愤慨。　　 <br />
　 　 <br />
万里政治上还是清醒的　　 <br />
历次党内斗争，他力求表现开明一面，但又注意“风向”变化，时常动摇，但到最后关头，他基本站到了正确的一边。外间对万里在1989年的表现有多种说法，赵紫阳一面之词是说：“万里对学运的看法和我自始自终都是一致的。”万里我直接找他谈过，他完全同意新的方针。”“5月3日我到万里家，同他谈了我对学潮的看法。”“他对我的意见完全赞同。”“5月9日他又到我家，说他出访加拿大和美国。本来他出国前，他要我找邓小平直接谈一谈。他时间来不及了。”“他一直支持开放民主、支持政治体制改革。”　　 <br />
万里预期5月22日从加拿大到美国，6月1日离开。到美国之前，国内出现两种声音，一种要他立即返回，一种是要他继续行程。在那个时刻万里是举足轻重的，他是人大委员长，但可以参加政治局常委会会议并投票。最终万里决定5月25日提前回国，这是一个事后有争议的决定。到达上海时，当时上海书记江泽民、市长朱F基和万季飞在机场迎接。但中央高层要他暂不回北京，他的专机只能停在上海。5月27日,中国各大报刊登万里名义文章，支持中央当时的决策。最终，万里并没有选择站在少数赵紫阳一边。5月31日，万里回到北京，受到彭冲等副委员长的迎接。再怎么风云变幻，万里都能审时度势，保持了与党中央的一致，显示政治上的某种成熟。　　 <br />
　 　 <br />
万里提“包产到户”并无实行　　 <br />
万里在文革后到安徽当省委书记，固然支持凤阳县小岗村农民的包产到户，众所周知，这是万里在中共党内的最大政治资本。但万里只是议而未决，他离开安徽后，“包产到户”一议并没有提到省委常委会讨论，安徽省委也没有作出任何决议或决定，反倒是，宋平当书记的甘肃省委在全中国率先以《关于当前农村工作的几点意见》1979.10.17.率先签订“包产到户”生产责任制。曾经当过周恩来政治秘书的宋平，一直没有去和万里争这个“谁是第一”。然而，宋平在甘肃其间当“伯乐”相中胡锦涛这匹“马”在北京时又支持地质部长孙大光提拔温家宝这匹“马”，对中国发展进程的功绩不言而喻。　　 <br />
赵紫阳1975年到四川当书记后就根本没有搞“包产到户”，他进行的是基本耕作方面的改革。他针对四川盆地冬暖夏早和肥料、水利条件，提出四川的基本耕作是稻麦一年两熟制，在丘陵地区，总结推广南充地区“水路不走走旱路”的办法，等等。赵为了这种改革，动用了库存的粮食。四川省原来有不少存粮，赵紫阳到任后，挖空了不少存粮，让许多农民得到休养生息。赵紫阳在其他改革的做法是，从地方上缴财政中，挖出一块给企业留成。当时在四川省担任财政局长的田纪云贯彻赵紫阳思路出力甚大，还拿六个工厂做试点。这是为什么赵紫阳在当总理后把田纪云上调中央当国务院副秘书长，之后进入政治局，升为副总理的原因。　　 <br />
<br />
因此，“要吃米，找万里；要吃粮，找紫阳”这句顺口溜是当时某个秘书发明出来的。严谨地说，这句话是事出有因，空穴来风，但并非全部事实。过度夸大万里和赵紫阳的历史作用是不妥当的。在当时解放思想的影响下，没有万里和紫阳，也会有别人跟进。　　 ...华岳论坛 - &quot;http://washeng.net&quot; <br />
　 　 <br />
万里在民族政策上的失误　　 <br />
1980年，胡耀邦、万里到西藏视察。5月29日，胡作了著名的“六条意见”讲话，至今仍然评价不一。最重要一句话：“感觉到我们党对不起西藏人民了！”胡讲话期间，万里中间插话达十二次之多，强力支持胡的看法。另外，胡认为，中央一年给西藏的5.6亿元钱，80%用在西藏的汉族干部身上。为了使中央的补助真正用于藏人，要把80%的汉人从西藏撤出。第三，后来胡决定把军人出身的区党委书记阴法唐撤换，调出当二炮副政委。1985年由赵紫阳派四川彝族人伍精华任书记，伍专走藏族人上层路线，两面不讨好。直到胡锦涛1988年到西藏才扭转局面。　　 <br />
胡耀邦个人优点缺点都很突出，缺点之一就是考虑不周，没有征求或顾虑其它领导人意见就贸然开炮，有时脾气也不小。但他对西藏的“六条意见”讲话，在八十年代引发“政治地震”达好几年之久。美国报纸通过私下渠道公开发表全文后，引发了境外藏人、藏独团体和支持他们的西方势力的欢呼雀跃。境内新疆、内蒙和青海也引发汉族和少数民族干部的内讧。有些汉族干部巴不得回内地或家乡工作，而少数民族干部趁机想挤走汉族干部。胡把新疆两种意见的干部召去北京开会，做了个纪要，结果会都开不下去。总理赵紫阳对此也不满意，说新疆问题怎么搞得这么糟。最要命的是，万里还在会议上表态说，新疆问题，中央只要三个权：国防权、外交权、内政否决权，即除了国防、外交归中央管外，新疆自治区的内政都由你们（当地维吾尔和汉人）决定，决定以后，中央只有这点权，即看到有哪些不妥当，可行使否决权，其他事情都你们决定。万里这些言论，脱离了中央的主流意见。但胡耀邦后来也意识到问题严重。在后来的西藏自治区谈会上，胡多次强调反“右”，显示他接受了教训，认识到他和万里的讲话是不妥的。　　 <br />
　 　 <br />
万里在八十年代其它代表性言论　　 <br />
1982年12月16日中共中央书记处研究室张云声不赞成南方沿海地区发展以雇工经营为特征的私人企业，写了一篇《到处出现雇工剥削引起的思考》，供中央书记处领导参考，万里看了很不高兴，斥之“胡说八道”。　　 <br />
1986年9月20日，政治局扩大会议通过《精神文明指导方针的决议》，其后9月28日十二届六中全会讨论中，陆定一突然举手“发难”，要求删掉“资产阶级自由化”这个提法，书记处成员中，只有万里一个出面附和，中央委员中还有小部分人鼓掌。万里讲：只要科学技术发展了，社会主义也不能搞自由化，资本主义也不能搞自由化。这个自由化，加到社会主义也不对，加到资本主义社会也不对，所以我赞成取消。　　 <br />
1987年1月2日，胡耀邦写信给邓，要求辞去总书记。1月6日，邓找胡谈话后，委托五人召集开“党内民主座谈会”解决胡的问题，即赵紫阳、薄一波、杨尚昆、万里、胡启立。1月8日，会议开了5天，和别人“猛烈”炮火批胡不同，万里的批胡显温和。　　 <br />
1989年9月4日，邓小平与政治局五常委加杨尚昆、万里七人谈话，表示要辞去军委主席职务，万里跟风表态支持。　　 <br />
　 　 <br />
万里与黄光裕兄弟　　 <br />
一般可以肯定，中国成功企业家背后都有一个高官。万里与黄光裕是通过万老四万季飞认识的，黄氏兄弟二人在内蒙古倒卖电器。失败，转到北京发展。通过中间人认识万里家人。黄发挥潮汕人公关送礼的传统，成为万里家的上宾。黄氏兄弟口头禅：向银行借100万，要用80万公关，20万营运。凭万里在北京市当过副市长、全国人大委员长的人脉，黄氏兄弟在北京很快争到了地皮，从此一“发”不可收拾。中间人后来都得到了黄氏兄弟给的一套豪宅。再拿件事实佐证：外界很纳闷为什么万里最疼爱的孙女万宝宝（万季飞之女），她既非演艺明星有收入，也无正式工作，哪里有那么多钱在巴黎上流社会当名媛、住豪宅，尽享风流？这些，都是由黄光裕兄弟的私下资助！现在黄光裕出事，万宝宝也乖乖回到北京。现实不就是这样吗？我们不能批评万里本人什么，但他家属的行为，对中共党内高层形象有严重伤害，当然包括对万里本人。因为中央一直要求高官约束其家属，万里就不该自律吗？</div>

							
							
							
							
