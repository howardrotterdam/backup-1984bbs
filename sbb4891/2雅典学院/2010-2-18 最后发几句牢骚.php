首先，这次事件中，我还真没看到有谁是要求你们封IP的，在我的观察中，包括我在内，我们都知道自己吃几碗干饭，从始至终我们都是建议，从来没有也不敢”要求“你们怎么样，其他人我不敢代表，但如果我的话被你理解为要求，那么我道歉，那不是我的本意。其次，我们确实是没有言论自由，我们不光没有言论自由，我们也没有尊严，P民的人格从来都得不到尊重，城管是如何打骂那些摆小摊的，熊猫是如何打骂上访者的我们都知道。我们不光在一个消息封锁的环境里，我们还在一个无视人类人格尊严的环境里，很多底层的人不光在生活中不能和周围的人交流想法，他们甚至不能得到别人的尊重。言论自由和对他人人格的尊重在天朝都是稀缺资源。<br />
<br />
三、尤其是那些傻逼左派们，不要再举例说外国人说脏话也会被管制，这种例子和逻辑只暴露出左派的软骨病。<br />
<br />
首先我必须要道歉，因为我要说脏话了，不知道这算不算是如你所愿，如果造成不快那请无视<br />
他妈的凭什么不能再举例说外国人说脏话也会被管制？嗯？妈了个逼的，说白了就是一举这例子你这二逼就没法反驳了吧？嗯？操你大爷的，选择性失明还能玩的这么光明正大，你他妈的知道要脸么？嗯？还他妈的”暴露左派的软骨病“，软你个鸡巴，就他妈你这二逼逻辑，充分的证明了你既不是傻逼左派，也不是傻逼右派，只是一傻逼<br />
然后我再次道歉<br />
再然后我想说，其实我一直所针对的不是辩论中的脏口，而是在辩论中突然冒出来的和辩论无关的人身攻击，以及专门发人参攻击的帖<br />
所以从他妈的一直到傻逼这段话，只是让你体验下SM的快感，算是新春礼物<br />
吐槽终了 然后说说对这个坛子的感想吧<br />
先说点题外话，大家花时间发起投票讨论为什么坛子没意思，正是因为大家都像书记一样，多多少少都对1984有些执着，所以才发牢骚，为什么人民网没人发牢骚？为什么我们不去猫扑发牢骚？我们苦口婆心花了那么多时间一起做这个事，我们为了什么？嗯？当然了，看到这的时候一定会有小二逼说你们爱来来，不爱来滚，欢迎你们退出1984，别他妈在这JJYY。这就叫不知好歹。你们把大家对1984的感情当什么了？嗯？不骂你们傻逼都对不起我职业道德<br />
<br />
然后转入正题<br />
<br />
来这个坛子一年半了，我刚来这个坛子的时候，这里有很多话唠，水平比现在要高，讨论中经常能带出很多有意思有深度的东西，但是现在，这些话唠几乎都见不到了，偶尔能看到他们的回复，但除了这几条可怜的回复之外，他们几乎都销声匿迹了。想过为什么他们都沉默了么？有很多种可能性，也许是他们忙了，也许是他们有妹子有快感了，但是现在，我的猜测是，他们也许认为这个论坛没意思了。我这么想的根据就是那个投票，100多个人参与投票，有百分之九十认为坛子越来越没意思，请注意是百分之九十，压倒性的大多数，几乎是所有参与投票的组员都认为坛子越来越没意思了。<br />
为什么几乎所有人都这个想法？我不代表其他人，只说我自己的看法，说的到不到的各位兄弟多包涵，不包涵也无所谓，随你们便。<br />
其一就是这个人身攻击的事，挑明了说吧，就是集中发生在刘身上的事。没错，刘的观点大家几乎就没几个认同的，包括我在内，几乎就全都不认同，尤其是刘的十二点事件，实际上我和各位一样讨厌这种做法，刘这是挟天子以令诸侯，因为这个小组见不得光，所以威胁起诉小组就可以震慑所有组员，是吧？但是对他的观点不认同，或者对这个人不认同就是侮辱其人格的合法理由么？嗯？好吧也许大家对刘和刘绍棠都有意见，认为他有诸多不对，BLABLA，但是再怎么不对，刘绍棠那也是他爹，说一句说两句就完了，我操，不管刘发什么帖子，说了什么话，总他妈的能扯到刘绍棠身上，只要刘一出场，最后必然是以刘绍棠作为结尾。关二是小孩，他还不懂事，就不说他了，但是我就操了，总他妈有一堆捧臭脚的，你们还他妈的分是非么？嗯？我记得曾经刘和关二在某个帖子里就这个问题集中爆发，结果站出来说关二不对的他妈的一只手都数过来了，我说不言语的诸位，你们是都认为关二那么说对么？认为关二说的不对又不言语的诸位，你们为什么不言语？是因为刘的观点得不到大家认同，刘总处于逆风局所以担心替刘说话会受到围攻么？<br />
这里面有一个言论自由的问题，我说了，言论自由是我的自由，那么人格不受侮辱难道不是我的自由么？侵犯了他人自由的行为，可以称之为自由么？如果可以的话，那么我杀人是否也是自由？有人说恶劣，这叫承担后果的自由，那在这个坛子里，谁他妈的承担后果了？<br />
需要再次强调的是，即使我这么说，我不会也不敢要求你们怎么做，充其量就是发牢骚而已。<br />
其二就是这个小组的走势，我们总说，天朝是没有常识的，这话真是太对了，天朝普遍没常识，无论左粪还是右粪，在我看来小组里目前比较流行的就是，凡是道德覆盖的但法律没有覆盖的，都是应该随风去的。凡是TG宣传的都是要踩烂的，敌人赞成的就是我们反对的，敌人反对的就是我们赞成的。<br />
其实TG说的那一套，刨去他的意图和他的做法，很多东西本身，注意是本身，其实是不错的，比如这个和谐，你看外国不和谐么？人家比我们和谐多了，人和人之间给与充分的尊重，人们有充分的言论自由，人们的环保意识很强，和自然和谐相处，这有什么错呢？但是在天朝，我们亲爱的档妈把和谐给演绎了，你说了我不爱听的话你就不和谐，你看了我不愿意让你看的你就不和谐，你不和谐我就和谐你，结果和谐就成了河蟹，还有档妈宣传的各种道德观念，在我看来除了集体主义和民族主义的那些以外，很多本身还都是没问题的，但是同样，档妈把他们都给演绎了，他们一方面要求下面的人遵守道德，一方面自己不遵守道德，于是道德就不再是道德，成了枷锁。也就是说，道德成了另外一种物质，但是我们很多兄弟，看到了枷锁，却把问题归结在了道德这种物质身上，于是重法制轻道德的观念就蔓延开了，因为TG给与我们一个极端，所以我们必然要用另一个极端作为回应。这就好像是我要买羊头，结果卖肉的给我的全他妈是狗肉，于是我就说，这他妈的羊头就是不好吃以后再也不买羊头了。<br />
所以很多人给我的感觉就是，他们认为你TG不道德完了还让我们道德，那我们也不道德了，我们不光对TG不道德，我们对其他人也不用再道德了，骂人是我的权利。如果他们替代了TG,那也只不过是从无间地狱跳到了阿鼻地狱<br />
说到道德，我之前说右粪也是没有常识的，意思是说他们在追求一些他们不知道的常识的时候，抛弃了原有的常识，比如对人的尊重，比如人身攻击是不对的，全世界都知道人身攻击是不对的，甚至制定了法律来惩罚诽谤（不是说我认为对刘绍棠的评价是诽谤，也不代表我不认为对刘绍棠的评价是诽谤，无论是不是诽谤都不影响我的观点。），这样的常识，我们很多尸油党的兄弟们就都不知道了。<br />
说到吵架，有人会说吵架是民主的象征，人家哪哪哪的议会还全武行呢，举个例子，以前看韩国议会打架的视频，人家议员打架是因为在野党为了不让一则议案通过，用柜子堵住会议室大门，禁止执政党进入会场表决。这你妈的也叫民主？所以别总把议会打架当民主，议会打架不一定就是民主，还有可能是专制呢，你们跟人家学点好行么<br />
再罗嗦两句。前些日子看易中天，他说道德这种东西是对社会所有成员都有约束作用的，包括对君主。其实这也是我一直以来的观点，道德（是羊头不是狗肉）这种东西和法制是一个国家健康发展的两只翅膀，少了谁都不行，道德作为一种软约束力对于是必须的，诸位可以看看实行民主制的非洲国家，因为物质极其匮乏，对于物质的渴望突破了这种软约束力，所以即使是实行了民主制，大多数人仍然想着尽可能的多捞点东西，这样的人基数一大，贪污腐败就严重了，即使他们是民主制。再看看美国，贪污了一两万美金就叫巨贪，就让美国人民惊诧了半天，他们为什么惊诧？仅仅是因为百八十年都没出现这么一个大贪官所以看着新鲜么？在我看来，道德法律以及社会的健康发展是互为因果的。我们需要做的是要让上面一块受到这软约束力和硬约束力的控制，而不是我们自己也和他们一样抛弃了软约束力。实际上一个物质的正常运转，往往依靠于各因素的平衡，TG抛弃了各种约束，反而约束我们，这样使得我们现在就很不平衡，我们需要做的是让不平衡的恢复平衡，所以我们呼吁法制，监督，等等，而不是以另一种不平衡来代替眼前的不平衡。<br />
当然了，某些兄弟又该说了，支那人就是爱被管，就他妈贱，说这话的各位，你也别追求法制社会了，你也别移民了，按你这说法，人西方法治社会，法律的执行是百分之百的，该怎么着就怎么着，被管的力度和精准性比咱这强百倍，你要是犯了事你在天朝送点礼说不定还能糊弄过去，你在人家那你就是找虐。所以想移民的各位就打消念头吧，你们那是犯贱。<br />
<br />
最后再说说1984本身，其实刚来的时候还是蛮开心的，那时候和这个坛子里大多数人都不认识，后来时间长了，渐渐的有些人就脸熟了，尤其是还能在这个坛子里遇到2次元的大家实在是让我很高兴，比如绝望姬，比如怪怪物，还有其他没点到名字的各位，我都记着呢。还有三次元的各位，有些人的思维水平还真是蛮高的，即使观点不一致，但是可以看出来他们是有头脑的（只不过拥有这种逻辑水平的人一只手能数过来就是了），和他们的辩论乃至争吵中我也修正了自己的思维，还有的人知识储备是真牛逼啊，我一个工科僧，看的书比他们远远不如他们多，差远了，看他们辩论所引用的论据，出现我不知道的东西的时候我也会去搜，托他们的福，我的知识储备比以前多多了，所以我对这个论坛还是有些感情的，确切的说，是对上面提到的这些个人蛮有感情的。比起新闻集散地，1984对我来说更像是一个可以和愿意扯淡的人一起随便胡扯的地方。<br />
该说的话差不多都说完了，书记说过，希望放弃言论自由的话可以给他短消息让他禁言，大概我上面的话会让书记以及其他一些兄弟感到不快吧，如果伤害到书记的感情，那么书记你可以把这些话解读为，我愿意放弃在1984的言论自由，这个号你就封了吧。<br />
对于之前说的所有伤害你感情的话，致以最诚挚的歉意。毕竟大过年的<br />
然后是，我在1984也参与了不少次轮战，骂了不少人，也说过不少错话，也让不少人着急上火了，最后在此一并致歉<br />
感谢大家一年半以来对我的关照，祝大家新年快乐<br />
