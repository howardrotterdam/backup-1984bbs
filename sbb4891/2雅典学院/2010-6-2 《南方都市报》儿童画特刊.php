													<h2>《南方都市报》删除六四漫画</h2>
						
						
																											<div id="postmessage_655258" class="t_msgfont">2010-06-02<br />
“六四”事件21周年前夕，《南方都市报》刊登一人阻挡坦克的儿童漫画。该报工作人员向本台表示，因漫画被读者恶意曲解和误读，因此予以删除。自由亚洲电台记者何平的采访报道 <br />
<br />
<br />
<br />
<br />
广州的《南方都市报》在6月1日“国际儿童节”特刊上，刊登一人站在坦克群前方阻挡坦克的漫画。这一儿童漫画在当天的报纸B16版和该报网站上均有显示，但不久即被删除。广州《南方都市报》的客服代表于6月2日通过电话向自由亚洲电台记者表示：“由于这幅漫画在网上被人恶意的曲解和误读，然后为了避免负面的传播效应，我们是主动把这个撕下来的。<br />
<br />
<br />
记者：那这个漫画为什么会被读者误解呢？<br />
客服代表：这个我没办法回答你。<br />
<br />
在1989年6月4日上午，经过昼夜武力行动的入京戒严部队坦克车队在进入天安门长安街时，被一名据称叫“王维林”的普通市民只身阻挡。这一画面被全世界的民众所目睹，多年来成为“六四”事件中中国公民反抗武力镇压的代表性一幕。在美国的中文政论刊物《北京之春》的主编胡平表示：<br />
<br />
“凡是了解八九的人都会对这个画面留下很深的印象。所以它已成为八九民运和六四屠杀这个事件的一个象征。因此《南方都市报》刊登这样很相似的这么一个漫画自然就会让人们联想到八九民运，联想到六四屠杀。当局呢绝对在掩盖真相，在官方的媒体上消除任何八九“六四”的一些事实。既然你官方装着没这么回事儿，大家都在装傻，但实际上它也把一个很重要的信息给透露出来了。”<br />
<br />
本周五，是中国“六四”事件的21周年。而中国政府至今对于“六四”事件的定论为“反革命暴乱”，相关的图片和事件报道也被严禁公布。原“六四”天安门学生领袖之一的柴玲说：<br />
<br />
“我觉得他们这当然是不明确的做法了。21年来都没有给六四有一个公正的评价。对六四受害者有一个正常的赔偿和对他们家属的抚恤。但是历史总会归还历史的真相，公正和正义总是会得胜的。继续这样打压下去是不明智的，也是对这些做决策的人，这些不承认的人创造一个不可扭转的历史潮流。这也是对他们个人在整个历史地位上面一个非常不易的一个写照吧。”<br />
<br />
在北京的不同政见人士江棋生认为，二十多年来中国政府极力掩盖和控制六四真相的做法，反映出当今执政者对“六四”事件的认同：<br />
<br />
“在我看来六四是一场军队对贫民的一个大屠杀。在当局的眼里，它觉得杀得对，杀得好。但是现在又不敢站出来承认，这么多年来一直是严密的封锁关于六四的任何东西。关键就在于21年前那场八九民运那是中国人民希望中国也要走向民主政治的这么一场伟大的运动。当时的统治者就用枪弹，就用屠刀镇压下去了。如今的统治者继续这样一种看法跟思路。”<br />
<br />
《北京之春》的主编胡平指出，中国目前公权与金钱相互勾结的严重社会现实，与当局不敢正视武力镇压民主诉求的“六四”事件有直接的关系：<br />
<br />
“八九民运我想不但在中国历史上，在世界历史上都是一场非常伟大的非暴力、和平抗争运动。而六四事件又是一个滔天的罪行。中国在经过了21年之后，对这件事一天不能得到公正的评价，中国就一天不可能走上一个健康的正常的一个道路。自从六四事件凭着这种血腥的道理，当局料定人们也不敢反抗。那么在这种情况之下呢从事的经济改革，自然就成了地地道道的权贵私有化，这也就是今天我们看到了中国现在的现状。”<br />
<br />
胡平认为，《南方都市报》删除儿童漫画一事表明，在六四21周年期间外界应对中国大陆媒体予以更多的关注。</div>

							
							
							
							
