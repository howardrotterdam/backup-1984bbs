胡子倒霉前夕，周不是没做过努力。薛明也找过他这位胡子“革命的领路人”。周也的确把他保护到某个安妥之处……但，后来毛大帅要下死手，周根本拦不住……无怪乎后来贺的追悼会上，他对薛明说：“薛明呀，我来晚了，我对不起胡子……”<br />
