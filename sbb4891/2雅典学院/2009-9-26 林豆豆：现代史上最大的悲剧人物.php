举目四望本朝军政界，周的门生故人遍及，这是周用来抗衡毛以自保的本钱。但被对方削弱本钱也是有底线的，一度，毛要下狠手收拾周的情治系统，但反弹力之大，令毛棘手，转而不了了之了。<br />
周是明白人。有些人可以让出去，让毛吃掉；有些人则不能。<br />
