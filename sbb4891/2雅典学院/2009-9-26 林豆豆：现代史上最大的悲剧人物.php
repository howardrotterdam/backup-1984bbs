													<h2>林豆豆：现代史上最大的悲剧人物</h2>
						
						
																											<div id="postmessage_259337" class="t_msgfont">芦笛 <br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;写完了《试解林副统帅仓惶出逃之谜》后，不由得沉浸在对林豆豆的巨大悲悯之中，和刘涛一样，她乃是那个灭绝人性的制度制造出来的最可悲的牺牲者。 <br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;读者大概可以看出，林副就坑在立衡与立果一对活宝手上。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;许多国外观察家不明白，９届２中全会后，面对伟大领袖举起的屠刀，百战百胜的天才元帅何以如此消极被动。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;的确，从表面上看，“牛耕田，马吃谷”，中央文革在前头亲冒矢石，辛辛苦苦打倒党官僚，却让林副一系在后面坐收渔利，纷纷出任各级枢要，以致林副三分天下有其一。既然如此，林副何不放手与伟大领袖一搏？以他之用兵如神，正未知鹿死谁手。 <br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;观察家们不知道，林彪派系确实在文革中急剧扩张了，但那不过是潜势力，在毛死后倒确实举足轻重，在毛生前却是画饼充饥。林根本调不动一兵一卒，有什么本钱和毛较量？毛掌握了所有兵力，而林连自己的警卫部队都控制不了，能成功逃出中国，就算了不起的“胜利大逃亡”了。 <br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;由此看来，林副在庐山会议后“清静无为”，其实是最佳决策。我已经指出，毛林是同一造神运动造出来的一荣俱荣，一损俱损的连体胎。这“造双神运动”既使得林无法公开反毛，也使得毛难以公开倒林。要明白后面这点，只需看看毛为了证明“到底是谁对不起谁”，被迫向全国人民公布那痛骂他“披着马列主义外衣，行秦始皇之法”的《５７１工程纪要》就够了。 <br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;即使有了那些“当面说好话，背后下毒手”的“证据”，林彪事件还是让毛五痨七伤，一病不起，再也没能从那沉重打击中恢复过来。那么，假设立果少爷什么也没干，林副也没仓惶出逃，毛到底能用什么理由，堂而皇之地把林彪整死？据说那是他老人家鉴于赫鲁晓夫篡党夺权的教训，考察了几十年才亲手选定的最可靠的接班人阿？ <br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;我已经指出，“批林批孔”运动完全是闹剧。从林彪的公开言论中，伟大领袖根本找不出任何把柄，因为那完全是毛思想最高最活的发挥，要批就只能批到自己头上。所以张姚只好乞灵于从毛家湾抄出来的条幅，根据什么“悠悠万事，唯此为大，克己复礼”、“天马行空，独往独来”、“每临大事有静气，不信今时无古贤”等片言只语，莫名其妙地将副统帅与孔老二挂上钩，以“批孔”来偷换“批林”那不可能解决的难题。如果林副不是仓惶出逃，则恐怕连这点“证据”都不会留下来，那张姚还能批什么呢？ <br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;这当然不是说伟大领袖会网开一面。立果少爷早就在《纪要》里说了，伟大领袖的特点是“一旦得罪人就得罪到底”。不动手则已，一动手就绝对不会半途而废，而他搞掉林的决心已经在南巡讲话中暴露无遗，特别是在９月１２日中午在丰台召见吴德等大员时，那杀气腾腾的口气，吓得吴德一怔一怔的，不知道该怎么传达那最新指示。 <br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;正因为伟大领袖开弓没有回头箭，我才想不出来他怎么去解决这不可能解决的难题：假定林副软顶到底，就是不悔罪，但也不自卫，伟大领袖当然可以把他抓起来，像整治刘少奇那样把他整死，但又该如何去向全党全军全国各族人民交代？那交代可是必不可少的，否则不符合他的流氓段位。 <br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;我想，这就是林彪何以毫无抵抗的原因吧。他大概看穿了这一点，发现自己和正统帅处于同一难题之中，彼此都让造神运动捆住了手脚，将死了军。可惜少爷见不及此，主动去帮正统帅解决了这一难题。 <br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;比起郡主来，殿下闯的乱子根本算不了什么。亏豆豆还指责李文普是毛安在林副身边的“卧底”，她才是伟大领袖安排在副统帅身边的活窃听器，亲手把她爹逼上黄泉路的克格勃。如果不是她大义灭亲，毅然举报她亲生弟弟和亲娘的谋反大罪，则那三人也不会不顾一切地出逃，完全可以从容不迫地布置得更周密些，因而也就更安全些，何至于葬身大漠中？ <br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;《三国演义》上，袁绍不用田丰之计，还杀了他，后来兵败，头给割下来送到千里之外去报捷。罗贯中作诗悼曰：“头颅行万里，失计杀田丰”。敬爱的林副的头颅行得更远，到了莫斯科，后人作诗悼之，应该是“头颅行万里，失计育两童”，也就是毁在立衡立果两神智不清的乳臭小儿身上。 <br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;“亲戚或余悲，他人亦已歌。死去何所道，托体同山阿。”无论是首长，是主任，还是林部长，都已暝目长逝，什么都不知道了。真正日日夜夜受良心煎熬的，还是林立衡女士，她才是这场悲剧的最大的受害者，是那灭绝人性的制度的主动殉道者。这就是她何以发誓将余生用在为她父亲平反昭雪之上──那是她唯一可以求得良心平安的手段了。 <br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;我到现在也无法理解那种革命家庭：林立衡受罪，始于立果向她和盘托出密谋的那一瞬间。作为无限忠于伟大领袖的革命战士，她当然不能容忍这种谋反大罪。为此，她可以毫不犹豫地向中央举报，并多次请求警卫部队把她亲娘和亲弟弟抓起来，而这只意味着两人将被军事法庭审判后枪决。对此严重后果，她那现役军人绝不会不知道，但她却毫不犹豫地这么作了，唯一的指望是保全她爹。 <br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;任何一个平民子弟都能看出这种幻想是何等滑稽：覆巢之下，岂有完卵？更何况伟大领袖早就卯足了劲要整她爹，而她也不是不知道这点（她完全知道庐山会议内容，更别说立果一定会向她传达过毛的南巡讲话），怎么还会产生这种天真幻想呢？但她就是从头到尾都坚信可以用牺牲她娘和她弟的方式保全她爹，事过几十年还执迷不悟，至今还在以那荒诞不经的说法坚持她的personal <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;crusade，以为可以说服现中央为她爹平反！ <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;世上怎么会有这种家庭？为何豆豆就是不能跟最宠爱她的亲爹开诚布公地谈一谈？为何不能先和她娘、她弟弟举行严肃谈判，以“我要向中央举报”的威胁，吓阻对方轻举妄动？哪怕是从毛的革命路线标准来看，这也是挽救同志的正确作法吧？为何连个最后通牒都不下，连让她爹娘弟弟悬崖勒马的机会都不给，就去背着家人进行阴谋活动，试图把秘书、卫生员、警卫部队等等拉过她那边去，无效后竟然向低于她爹的“中央”举报？难道与自家亲人沟通，竟然是毫无可能的事，以致她必须借助外力来解决家庭纠纷？ <br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;悲剧在于，这种怪事在革命家庭中决非绝无仅有。当初刘涛同志就曾欺骗其继母王光美，电话报告她说刘平平遇到车祸，重伤入院，吓得光美同志直奔医院，被早等在那儿的清华造反派“智擒”，押到清华去百般侮辱，用乒乓球做了个项链给她戴上，弄了件烂旗袍让她穿上，拍了许多丑态百出的照片流传全国。刘涛还动员她生母写了许多大字报，揭发少奇同志的许多隐私。诸如什么少奇同志嫌她生母手上有伤疤难看，要她戴手套遮丑等等，那脍炙人口的“金鞋拔”就更不用说了。 <br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;记得我当时看到那些丑闻时，觉得其实“新”社会的一夫一妻制还不如“旧”社会的一夫多妻制。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;在一个男性社会中，“男人富贵必易妻”乃是客观规律，你承认它是那样，不承认它也是那样。“旧”社会承认这点，实行一夫多妻制，让男人在事业成功之后既能如意获得新欢，又保留了糟糠之妻的崇高地位，完美实现了老中青三结合。而“新”社会不承认这个客观规律，于是不是制造出贺子贞那样的疯子来，便是制造了少奇和林副的前妻那些不共戴天的仇敌。 <br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;这当然只是题外话。我死也不明白的是，血缘形成的强大纽带，为何会在革命教育面前脆弱到不堪一击的程度？无论是刘涛还是豆豆，背叛家庭似乎就跟上街买东西一样自然。豆豆在举报她娘和弟弟的杀头大罪时，似乎根本就没有遇到什么不可跨越的感情障碍。 <br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;说到底，豆豆的唯一烦恼，似乎就是中央不该冤枉了她爹。假定伟大领袖满足了她这一合理要求，及时下令８３４１部队把叶群和林立果抓起来，组织军事法庭审判之后，枪决了两人，同时令副统帅与他们划清界限，以此作为保留他位置的前提，而副统帅也从命了。那么，豆豆是不是就可以心满意足，此生再无遗憾了呢？ <br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;还不止此。无论是豆豆还是前驱刘涛，都在事后无任痛苦地发现，其实出卖亲生父母，并不可能换来伟大领袖的信任。两人都只有瞬时利用价值，事过境迁便立刻弃之如敝屣。林副才上天，汪东兴便在电话中对立了大功的豆豆毫不客气，指责她报告太晚，说现在没空跟她多说，便粗暴地挂了电话，竟然连句安慰话都没有。此后豆豆更被我党审查多年，终生成了怀疑对象。如果我党不是如此薄情，而是赏以重任，将其提拔到王洪文那样的位置。那么，豆豆今天还会把为她爹平反昭雪当成余生的唯一目标么？ <br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;悠悠苍天，此何人哉！此何人哉！ <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;在毛共执政３０年中，类似的悲剧不知道发生过多少。许多热血青年在土改中带头斗争自己的地主爹娘，导致父亲被活活打死。后来才无限痛苦地发现，自己非但没因此赢得我党赏识，反倒成了“杀关管”家属，属于“与我党有杀父之仇”的天然怀疑对象。刘涛、豆豆与那些黑崽子的区别，只在于“革命革到自己头上来了”而已。 <br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;敬爱的林副统帅英明地指出：“这次无产阶级文化大革命，就是要革过去那些革过命的人的命。”这话胜过了春桥同志的千言万语，一针见血地指出了伟大领袖毛主席“无产阶级专政下继续革命的伟大理论”的实质。 <br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;我已经反复指出过，国际共运之所以是人类历史上最邪恶、最灭绝人性的反动事业，恰在它在“道义”上的“完美无缺”。它比法西斯更具有欺骗性之处，就是它“代表被压迫被剥削人民”的“正义性”。列宁主义的要义是，只要是出于广大人民的利益需要，就什么无法无天的烂事都可以干，什么下流手段都可以使出来，什么冤案都可以制造，什么人都可以杀害，那乃是为了解放全人类必须付出的正当代价，用思云的话来说，乃是必须打破的“一些坛坛罐罐”。 <br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;这种理论必然把“人民利益”的解释权与代表权归结于共产党，最后归结于党领袖一人。因此，斯大林主义是列宁主义的必然归宿：列宁只是以革命名义屠杀党外人士，而斯大林则发展到了以革命名义屠杀党内所有的假想敌，这一切犯罪活动都在“人民利益需要”的借口下得到了合理解释，这就是毛指出的“列宁和斯大林这两把刀子”的实质。 <br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;伟大领袖在这上头其实没有什么独创性，他完全是斯大林的忠实弟子。据他自己说，他看了四遍斯大林主持编写的《联共（布）党史简明教程》，受了深刻教育和启发。那启发是什么？一曰重写历史，把苏共所有的成就都说成是斯大林一人的功劳。二曰“路线斗争”，亦即把党内的政见分歧乃至权力斗争都说成是敌我斗争。任何人胆敢不同意斯大林，哪怕只是在策略上的轻微歧异，或是让他感到自己的权位受了威胁，都要被当成间谍无情消灭。 <br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;这就是毛的“路线斗争”的理论来源，也是文革爆发原因。他唯一有别于斯大林之处，乃是后者习惯于欧洲式高效率，直接把权力斗争对手毙了了事，而毛则使用暴民运动来收拾他们。毛的这一发明，使得全民毫无必要地介入了高层权力斗争，极大地败坏了人民的心术，因而以社会化大生产的方式，制造了刘涛、豆豆那种灭绝人性的牺牲品。这些人坚信，世上最高的正义，就是伟大领袖代表的广大人民利益，为了这至高无上的正义，父兄可卖，亲娘可弑，没有什么事是人不能干出来的。 <br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;吊诡的是，正是伟大领袖发动的文革，拯救了许多高干子弟甚至高干本人，使得他们找回了迷失已久的人性。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;文革后出了许多“伤痕文学”作品，其中最感人的，乃是陶铸女儿陶斯亮写给她爹的《一封没有寄出的信》。我至今难忘的乃是其中一个微不足道的情节： <br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;陶铸倒楣后，全家自然跟着倒楣。作父亲的当然知道我党的厉害，他那独生女儿从此再无前途可言，找对象是再别梦想了，而她不幸已经到了待字闺中的年龄。 <br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;不料一日陶斯亮却对她爹说，有人追求她。他喜出望外，赶快向她交代自己的问题，为此受尽折磨：说重了，怕吓跑了对方；说轻了，又怕成了欺骗对方。交代完后，陶斯亮乃出去见她那新交上的男友，留下老头在屋里团团转，揣测此行吉凶。好不容易盼到他女儿回来，得知对方并没有给他的严重政治历史问题吓跑，不由得喜心翻倒，连声大叫：“我的亮亮有男朋友了！” <br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;当年看到这一段，泪水婆娑，打湿了报纸。那一瞬间，我看到了在神州大地上无数次发生的黑六类们的悲剧，竟然在无产阶级革命家家内一模一样地重演，当下只想放声高呼：“文化大革命就是好，就是好，就是好来就是好！” <br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;为什么好？因为它请君入瓮，让那些铁石心肠的革命家尝到了千千万万被镇压、被清算、被侮辱、被压迫、被损害的革命对象的滋味，让他们平生首次获得了设身处地换位思考的难得机会，亲身感受革命的血腥残忍，亲自体验那制度的灭绝人性，从而获得了找回久已迷失的人性的宝贵机会。这应该说是老邓复出后不但给老干部平反，还为黑六类摘帽，从而结束了中国历史上比纳粹迫害犹太人还丑恶的一章的心理原因吧。 <br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;的确，在各级干部子弟的回忆录中，无论是《鸿》，还是《点点回忆》，我都看到了这种被扭曲、被压抑的人性复苏。从这个意义上来说，其实文革中被整的革命家庭，远比那些逃过文革风暴的干部家庭更幸运。无论是张戎，是陶斯亮，是罗点点，还是刘少奇家的大部分子女，良心都远比豆豆更清白，人性都远比豆豆更健全，余生当然也就比豆豆更平安喜乐。 <br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;正因为此，豆豆才是中国现代史上最可悲悯的悲剧人物，良心上压了父母和弟弟三个血亲三条命，我实在无法想像一个人如何能从这种无边黑暗中挣扎出来。 <br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;人世最可怕的邪恶，乃是以罪恶手段推行的完美无缺的正义事业。世间最可悲悯的悲剧人物，乃是自觉主动献身给这种正义事业的崇高殉道者。 <br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;２００６年１月２６日<br />
