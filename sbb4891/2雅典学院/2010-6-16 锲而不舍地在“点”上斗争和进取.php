													<h2>锲而不舍地在“点”上斗争和进取</h2>
						
						
																											<div id="postmessage_685133" class="t_msgfont">英国的自由主义思想大师俯拾即是，斯密、洛克、休谟、穆勒、柏克、边沁、霍布斯，等等等等。法国则不然，除了托克维尔和贡斯当（或许还有孟德斯鸠），能称得上自由主义大师的恐怕只剩基佐这位历史学家了。20多年前我读过他的《1640年英国革命史》，08年读了他的《欧洲代议制政府的历史起源》，这一次真正理解了这位大师的思想精粹。<br />
<br />
基佐的研究方法既不像哲学学派那样以理念先入为主，结果难免用选取的事实来套论理念的正确；又不像历史学派那样只以事实为准，而忽视了一些先验的却又正确的理念。基佐是用注意保持理念和事实平衡的方法，来研究欧洲代议制政府尤其英国政府及议会的历史起源。论述精彩纷呈，结论令人信服。<br />
<br />
比如，基佐对英国历史的研究有这么一个结论：没有贵族院就没有贫民院。也就是说，权利总是从特权阶层这个高处（或内部）逐步向下、向外扩展开来，最终达到普遍人权。<br />
<br />
让我们看看基佐的这段话：“实力与社会地位的巨大差异，是英格兰贵族院制度产生的唯一起因。它的形成与任何政治图谋或公共权利思想都没有关系。在此问题上，一定数量的个人的重要地位为他们创建了权利。”基佐的论证大略是这样的：假如贵族分散在各自的领地，贵族们就可分别向其属下施展绝对权力。而当贵族们聚集到贵族院时，每个贵族都差不多，谁也无法只凭自己的力量对其他贵族颐指气使，但要想使自己的主张得到其他贵族的认可，就不得不搬出冠冕堂皇的所谓公共利益作掩护（其实是借助院外的力量即公众的力量）。于是，起初只为贵族自己的争权夺利，客观上却把权利扩展到了贵族以外的人群。用基佐的话说就是：“一个由单个的上层人物组成、只热心于增加自己权力的议会，逐渐变成一个在很多时候不得不使自己服从所有人利益的国家机构。”<br />
<br />
基佐的这一论述比卢梭的“人人生而平等”符合实际得多。因为家庭出生的不同，体力和智力的不同，人是生而不平等的，这是显而易见的。后天努力的不同以及机遇的不同，也造成人与人之间巨大的不平等。不平等是人类族群从初期的野蛮迈向后来的文明的动力。自然界的热力学第二定律――熵，也在说明不平等（衡）导致运动（进步）、平等（衡）反而导致静止（停滞）的道理。其实，直到今天，人类的不平等也充斥着社会的各个角落，即使“法律面前人人平等”这样迷人的说辞也并不完全符合实际。美国黑人1969年才获得选举权以及前几年对辛普森的无罪判决都是例证。也就是说，至少某些权利不是天赋因而是平等的，而是争来的，并且是渐争渐得的，而不是一次到手的。<br />
<br />
纵观基佐对权利产生过程的描述，我称之为“权利渐扩论”。将其缩小到企业，可以看看富士康的大幅加薪。就中国大陆而言，外资企业就是企业界的贵族，它财大气粗，员工工资也比同类内资企业稍高，本次一次性大幅度加薪，也只有富士康这样的外资企业可以做到，也只有其员工才可最先享受到加薪的好处。由它带动和示范下，加薪潮才可能扩展到其他性质的企业。其实何止薪金权利，就连罢工这样的权利（如果真是一种权利的话），不也是先从外资企业开始付诸实践的吗？可见，企业的不平等也会导致权利的不平等，并使权利的普遍赋予由高到低、由洋到中地扩展开来。当然，富士康此次大幅加薪或许还有一种不惜以身试“市”的赌气在里头，不妨稍加妄析如下：<br />
<br />
工资也是价格，是劳动力的价格，它由劳资双方协商而成，且也有其自身的市场均衡。因为自杀频发，富士康被指血汗工厂，舆论几乎一边倒地谴责资本家的无良。结果，郭台铭来了个“非理性”应对――你们不是谴责我残酷剥削吗？那好，我就大幅加薪给你们看，看看我这非理性的破坏薪水市场均衡的行为能否堵住你们的嘴，看看薪水市场能否赶得上我的加薪节奏，赶得上（全行业乃至全民皆加薪），那是我郭台铭带头的功劳，赶不上以至闹起工潮可别怪我有什么“恶意”的阴谋，总不能让我里外不是人吧。<br />
<br />
富士康的加薪会否引发意外无须继续分析，但，多多少少的加薪已然成潮以至全民受益却是不争的事实。这，不能不说是富士康这个“贵族企业”内斗（舆论所指的员工不惜以死抗争）进而向外扩权的结果。至于加薪会否成为狂潮以及所谓国民收入倍增计划会否事与愿违，那是另一回事，此处不论。<br />
<br />
现在让我们把“权利渐扩论”还原到国家层面。<br />
<br />
中共建政后的历史可分为前后两个30年。前30年是彻底革命和继续革命的30年，是迷信“理性无限”从而全盘否定历史经验和传统文化、企图一夜之间建成共产大厦的狂妄的30年，结果造成民不聊生。后30年是学走正路的30年，是抛弃理性迷思进而以经济建设为中心的30年。其间也有多次反复，比如处理风波、治理整顿、打击水车工以及目前的宏观调控、国进民退等。但与前30年相比，进步巨大。<br />
<br />
当然，中国目前不如人意的地方依然很多，贪污腐败、分配不公、城乡二元、徇私枉法、欺压百姓，等等。尤其在借助互联网可以方便取阅信息并与西方现成模式横向比较的年轻人看来，现状简直惨不忍睹。农民选举权的完全落实（一人一票）直到去年才实现（此前4人算一票）之类的事，在他们看来完全是不可思议的耻辱和丑恶。以至有人成天想着改朝换代，嚷嚷暴力革命者亦不乏其人。《08宪章》虽然不是暴力解决论，但还是明显存有60多年前中共对中国社会连根拔起、全盘改造的错误痕迹，迷惑了不少人。<br />
<br />
看看基佐的“权利渐扩论”，你就不会对美国人没在黑人获得选举权的1969年之前想着推翻美国制度感到奇怪了。当然，你可以不把“权利渐扩论”看做人类历史发展的普遍规律，但英国人甚或美国人的权利史就是这样的。而且，依照渐进原则而来的国家，多数都顺利地走向了自由和繁荣之路。比如日本，其类似两次英国革命（1640年和1688年）的明治维新，早已奠定了虽有暴力（较少）但绝无全盘推翻的演进路基。日本妇女的选举权也是1947年宪法生效后才获得的。日本对本国特色的保护也是那么“顽固”，以至西方文化反而被日本文化所同化。这不能不说是日本社会尊重历史经验和传统文化的习惯浸淫的结果。<br />
<br />
接受了“权利渐扩论”，就不妨把中国党权下出现的权贵集团，看成类似英国王权下形成的贵族集团，说不定权贵斗争所立的牌坊最终可以扩展为所有人的普遍权利。当然，承受其间的转型代价是痛苦的，转型能否尽速实现也都没有十足把握，因为一个民族就像一个人一样，也有“运”的成分，运好，一切都顺，运不好，则波折多多。但，历史的经验告诉我们，一夜之间连根拔起全盘改造的办法多半都是恶果连连。本文的主旨就在于：不要把心思放在迷信理性无限而去向往彻底革命并企图一劳永逸地解决问题上，那是中共30多年前犯的错误。而要像解救邓玉娇那样，审慎地利用有限理性的力量，与中共一起博弈建构，锲而不舍地在“点”上斗争和进取。如此，或许可以助我们的“运”好很多。<br />
<br />
此外，拜互联网所赐，信息相对自由的传播，也许能加速实现我们的梦想――起码先把我们的下议院建在网上。况且，从最近几年网上一有贪腐揭发（比如天价烟局长）即有中共重拳打击，和几年前就已开始的中共主导的传统文化回归，都可以看出，中共是真心放弃了马列，并为当年彻底革命所铸成的大错而反省过，它只是已经或正在洗心而未革面罢了。如此，相比那些成天咒骂传统文化的人，相比那些沿用中共几十年前的思维而反共的人来说，中共却已走在了前面。<br />
<br />
不想多说了，如果你对权贵仍存的恣意妄为还心有不甘的话，我也只能在结束本文前请你再次重温基佐的这段话：“实力与社会地位的巨大差异，是英格兰贵族院制度产生的唯一起因。它的形成与任何政治图谋或公共权利思想都没有关系。在此问题上，一定数量的个人的重要地位为他们创建了权利。”总之一句话，历史形成的巨大不平等，转型时就必须承受其被削平前的肆意所造成的代价。<br />
