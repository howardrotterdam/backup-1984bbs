早前的新闻<br />
<br />
外电昨引述市场消息人士透露，国寿（2628）首席投资官刘乐飞，已被提名为A股中信证券旗下中信产业。投资基金管理有限公司的董事长。中信集团高层内部已获悉有关提名，不过，刘乐飞仍未完成交棒工作，国寿仍未决定其继任人选。<br />
