													<h2>芦笛：刘晓波的悲剧</h2>
						
						
																											<div id="postmessage_403905" class="t_msgfont">刘晓波的悲剧 <br />
芦笛 @ 2009-12-29 16:36 阅读(472) 评论(1) 推荐值(28) 引用通告 分类: 未归类 <br />
刘晓波被判刑前一天，一位朋友打电话给我，说刘晓波次日就要审判了，他估计会判得很重，盖中央指望以此杀一儆百，吓阻国内异议人士，否则他们就有可能组党。他估计，中共在重判刘晓波之后，关押上一段时间之后，便会把他放逐出来，使得他在“海外民运化粪池”里自动融化，彻底失去影响力。 <br />
<br />
这位朋友消息灵通，很有洞察力，他的话我不能不信。次日特地上网，果然不出他所料，刘晓波给判了11年，顿时只觉说不出的悲伤和沮丧，这些天来什么心思都没有，觉得中国真是个被上帝诅咒过的国家，永远在那既定轨道上运行，一点改弦易辙的指望都没有。 <br />
<br />
后来就看到了判决书，佩服到五体投地。我过去总认为无论中共做出什么事来，我都不会惊讶，不料拜读了那伟大的历史文献，这才发现自己过去严重低估了我党脸皮厚度――竟然连这种不要脸的弱智玩意，他们都有本事在全世界面前亮出来！ <br />
<br />
据说，刘晓波犯了“煽动颠覆国家政权罪”，全部证据是两条，一是《零八宪章》，二是刘在境外网站上发表的六篇文章。这种白痴玩意都有本事拿出来现世，令人不能不佩服我党果然是无所畏惧的彻底的唯物主义者，没有脸皮一说。 <br />
<br />
如果说《零八宪章》是“煽动颠覆国家政权”，那么，这就是个涉及到一万人以上的集团犯罪。就算刘晓波是集团军司令，那起码也得把所有的从犯都抓起来，作为一个叛国大案来审讯，以查明各人的犯罪性质与程度并作相应判决吧？只审判刘晓波，余人不问，岂非玩法轻纵了那一万多名叛国罪犯？这就是党国的法律尊严？ <br />
<br />
那在境外网站发表文章的“罪证”更是令人笑掉大牙。我做梦也没想到，《中华人民共和国刑法》竟然能管到境外去！文章既然发表在境外网站，那么，它是否犯法，当然只能由网站所在国判定，岂能由中华人民共和国的司法机关悍然侵犯其他国家的主权，强行代替其他国家操刀执法？“境外犯罪境内审”，党国政府发明的这新时代的“治外法权”，是不是也太特别了些？这一套，就连当年的英法帝国主义者也没干过吧？莫非今日中国真是成了独霸世界的超级大国，可以任意侵犯别国的司法主权？ <br />
<br />
尤可笑者，判决书还承认，刘晓波那些文章是从2005年以来开始发表的。那就是说，这叛国犯罪行为持续了三年之久。那为什么北京市公安局公共信息网络安全监察处要包庇他持续犯罪，既不立即制止，甚至也不加以警告，而要无所事事，坐视他猖狂煽动颠覆国家政权达三年之久？包庇犯罪，本身就是犯罪，可见公安局乃至公安部才是包庇纵容刘晓波犯罪的幕后黑手，才应该被抓到被告席上去。 <br />
<br />
还不止此。如所周知，我党动用大批民脂民膏搞所谓“金盾工程”，把伟大社会主义祖国严丝合缝地密封起来，连电子都没法穿透那网上柏林墙。刘晓波的文章发表在海外，国内网民根本就看不见，那还有什么“煽动”一说？那几篇文章的总点击率最低为57，最高748。阅读者只会是海外华人，并非中国公民，就算是中国公民，就那么几个人，还都在海外，怎么个颠覆中国政府法？连这都算犯罪，那在梦里批评共党也是犯罪了。我党是不是还该投入重金，研发“梦幻监测工程”，好把每个公民梦中的反动言行都及时记录下来，必要时用作法庭审判依据？ <br />
<br />
如果向境外网站投稿算是“煽动颠覆国家政权”，那我党为何还要提供经费，让居住在境内的前“民运”垃圾胡安宁在境外开办网站，四处剽窃批评我党的文章，在该网站大量刊登并提上导读呢？那罪行是不是比向境外网站投稿更严重万倍？ <br />
<br />
当然，胡安宁这次大概是接到指示，除了刊登判决书之外，便是登出《北京日报》在六四后不久发表的所谓刘晓波的反革命言论，还特地把日期抹掉，企图让读者以为那是刘最近的言论，使得我党“煽动颠覆国家政权”的指控更像回事些，却不敢转载声援刘晓波的只言片语。不容否认，胡确实为我党正面宣传此案作了密切配合。但他在此之前犯下的严重的“煽动颠覆国家政权”罪行，总不能因此就一笔勾销吧？ <br />
<br />
总而言之，我党这次出的丑，堪称新时代《笑林广记》之最。无耻和弱智都是我党的行事常规，在一般情况下绝不会使我惊奇，但这次中共居然脸皮厚到如此地步，敢把这白痴判决书公诸于世，却不能不令我倍感绝望。这涵义连盲人都能看出来：如今我党是有恃无恐，再不把文明世界放在眼里了。 <br />
<br />
刘晓波判刑如此之重，堪比魏京生。但魏京生好歹还有个什么“出卖国家机密情报”的借口，而且政府始终没敢把那判决书亮出来。那还是在铁血强人邓小平当国之时。如今胡锦涛不过是个积极分子出身，刘晓波的言行中根本就抓不到什么把柄，但胡就是不但敢重判刘晓波，还居然敢把那白痴判决书公诸于世。他敢这么做，当然不是因为其魄力堪比老邓，而是国力今非昔比，中国已非昔日之吴下阿蒙了。过去中国必须仰赖西方的金钱和技术，因此行事不能不有所顾忌，如今据说中国成了救世主，西方正翘首盼望中国广撒金子，把他们从水深火热中拯救出来。既然如此，党国政府还有什么可忌讳的？ <br />
<br />
这就是第一个令人绝望之处――再也没有管束那个无法无天的流氓政府的国际压力了。 <br />
<br />
第二个令人绝望之处，乃是中共通过此举，向全世界明确无误地宣布了他们绝不会允许体制内的反对派出现。判决书说得明明白白：“反对＝颠覆＝犯罪”。中国人只能有两条路可供选择，要么当奴才，要么做响马。谁若想向刘晓波那样走出第三条路来，坚持做体制内的反对派，谁就是罪犯，就要遭到“人民民主专政”铁拳的无情痛击。 <br />
<br />
这才是最令人悲哀之处。我在旧作中反复说过，中国人只会走极端，不是当奴才，就是作响马。不是逆来顺受，俯首帖耳，听任暴君宰割，就是扯旗造反，试图推翻暴君，却从来不知道，无论是当奴才，还是做响马，社会都不会因此前进一毫微米，反而只会倒退。要想建立一个常态社会，就必须在政府之外有个“忠诚的反对派”（也就是体制内的反对派）。没有这种拮抗机制，则社会只会一天天烂下去，不知伊于胡底。 <br />
<br />
这就是老子说的“反者道之动”。就连传统社会的君王都知道这浅显道理，为此特地设置御史台，构成古典的体制内反对机制。尽管那设计的作用与效果非常有限，但毕竟还是说明古人有点起码的政治智慧。 <br />
<br />
中共却连传统帝王都不如，专门提拔奖励无耻拍马的佞臣，专门整肃直言敢谏之士，就连刘宾雁那种拥护共党领导、相信社会主义的现代御史都容不得，要把他放逐出国，连死后都不许埋骨乡梓。中共更把阿谀奉承统治者当成中国公民的责任和义务。谁若是敢批评我党，不管动机如何，统统都是犯了叛国罪，都要以大牢伺候。通过刘晓波案，我党已经把这基本国策再次说得清清楚楚了。 <br />
<br />
这是何等可怕的前景！一个国家的统治集团毫无起码约束，为所欲为，只许人民规规矩矩，不许人民乱说乱动，若要乱说乱动，坚决镇压。这种毫无刹车的国家会开到什么样的深渊里去，恐怕就连白吃也能想得出来吧？ <br />
<br />
本区网友正在争论这道义与功利的问题，却没看到这两者根本就不是对立的。所谓道义，说到底就是全民的功利所在。我反对的，乃是如伪民运良心家们那样，把道义从功利中剥离出来，使之变成一种超越于功利存在的绝对实体，当成一种政治斗争武器，并不是认为可以只讲个人的功利，不顾全民的功利（亦即道义）。脱离了全民功利考虑的言行就无所谓道义，而民运良心家们的绝活，正是要求人民不顾自己的身家性命，去为他们主观制定的“道义原则”作牺牲。但不能因为有这些骗子良心家们的存在，就否定出自全民功利考虑的正义行为。 <br />
<br />
从个人的角度来说，刘晓波坚持在国内当体制内的反对派，试图首次在中国走出响马与奴才之外的第三条路来，当然是功利上的极大失策，但从全民的角度来看则代表着国家民族的根本利益所在。现在中共悍然再次宣布此路不通，中国公民若不是奴才，就只能是敌人。在取消自我完善的一切可能之后，这种烂污社会还有什么痊愈希望，还有什么长治久安的可能？于是医治社会病态的唯一希望，便是大家集体做响马，再来一次翻烧饼，再使社会来一次大倒退。功利家们为何就看不到这彰明较著的恶果呢？ <br />
<br />
这就是刘晓波的悲剧所在：在人山人海的奴才与响马大军中，他代表道道地地的“一小撮”特立独行之士，势必遭到朝野的两面夹击，其遭遇正如当年俄国十月革命前的立宪民主党人与进步党人一般，因为试图在体制内推进民主改革，最后是既得罪沙皇，又被激进革命乱党蛊惑下的愚民最终无情抛入“历史的垃圾堆”。 <br />
<br />
的确，在一个愚昧的国家，刘晓波这么干，只能两面不讨好。《零八宪章》固然是我党的眼中钉，但它同时也被海外响马们痛加抨击。以《大纪元》为喉舌的法轮功宗教政治党人，以及何清涟乃至高寒那群形形色色的草寇，一直在辱骂《零八宪章》，污蔑刘晓波是共特。 <br />
<br />
因此，说到底，刘晓波的悲剧，其实是民族的悲剧。它响亮地宣布了“改良主义行不通”，而这是朝廷与草寇联合作出的英明决定。中国的事，从来就是糟在这两大势力手上，不是毁于朝廷，就是毁于草寇。晚清是朝廷要改良，草寇不许改良，八六年、八九年也这样。但六四以后则是朝廷坚决不许改良，草寇则历来不许改良，倒也没什么立场变化。 <br />
<br />
正因为此，刘晓波被重判反而是草寇们的盛大节日。要知道这一点，只需在Google里打入“刘晓波革命”的字样，立刻就能看到草寇们的鸦鸣雀噪： <br />
<br />
“重判刘晓波是民主革命的反面动员令” <br />
<br />
“和解破裂，革命将起――中共重判刘晓波博士将引发时局动荡” <br />
<br />
“刘晓波和08宪章：幻想的破灭……这一次，《08宪章》和刘晓波的最大意义，正在于宣告《08宪章》和刘晓波余杰们那种鼓吹和解合作、反对革命的政策和路线的又一次大破灭。” <br />
<br />
“‘11年’，是《零八宪章》的悲哀，是刘晓波们的失败。刘晓波们应清醒了！” <br />
<br />
看着这些意淫昏话，我不禁要觉得疑惑：刘晓波为这种愚民牺牲，是不是也太不值得了些？革命？革甚鸟命！时局动荡？动甚鸟荡！诸位的屁股还让海风吹着呢，哪来的金钱去造反？有谁会听你们的？如今大陆知识分子全是利欲熏心的奴才，人家忙着搂钱还来不及呢，顾得上管这些不相干的事？而没他们的组织领导，工农大众不过是一堆肉而已。 <br />
<br />
这就是第三个绝望之处：改良之路走不通，革命之路更走不通。在旧中国，人民好歹还有当奴才或作响马两种选择，如今却只剩下了当奴才一种。而这必将使得我党更加有恃无恐，毫无顾忌，于是政权就只会一天天烂下去，一直烂到党内内讧大规模爆发，将中国再度投入血海而后快。 <br />
<br />
最后想说的是，胡紧套看来是铁了心了。从我党的私利来看，老邓的确没有选错人，积极分子历来比高干子弟更左更可怕。在他手上，我党绝不会放松控制，只会王小二过年，一年紧似一年，而这确实能保证政权稳定。我在旧作中反复指出过，统治者是否具有足够的镇压意志，对政权是否垮台常常起到决定性作用。如果1991年苏联“八一九政变”的发动者们有胡锦涛的镇压意志，叶利钦就只会乖乖授首，而苏东帝国也就不会土崩瓦解。 <br />
<br />
在这种情况下，如果民运领袖有谁想把此事闹大，建议他们这就前往中国，陪刘晓波一道坐牢。据我模糊记忆，民运领袖中颇有人在《零八宪章》上签名的。既然国内都有人去投案愿和刘晓波一道坐牢，那签过名的民运领袖们就更该如此了。若是只敢作隔岸煽火的“远距离革命家”，那最好从此把“道义”二字收起来。 <br />
<br />
另外，冯正虎的斗争可能也不会有什么结果，我党不会让步的，他们有恃无恐，根本就不要脸了。这也算“人至痞而无敌”吧。 <br />
<br />
呜呼！</div>

							
							
							
							
