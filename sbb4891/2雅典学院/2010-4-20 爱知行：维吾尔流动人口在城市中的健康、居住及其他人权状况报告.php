该目旨在帮助中国各个城市生活的维吾尔流动人口，获得关于健康的教育，包括艾滋病及防治基本知识、毒品预防教育、减低伤害、肝炎防治、结核病防治 等；帮助获得基本的医疗服务，特别是艾滋病病毒感染者获得免费的医疗和检测服务、吸毒者获得政府提供的美沙酮维持治疗等；帮助维吾尔人更好的溶入随在城市 和社区，推动其他民族的中国人更好的认识维吾尔人的生活和健康危机等。<br />
<br />
内地维吾尔流动人口健康教育项目2009年以来，针对内地维尔族社群，采用同伴教育方式，通过社会学、预防医学、法学、心理学、行为学、等多学科结 合的角度开展健康教育、法律援助、艾滋病转介与检测、美沙酮社区转介、基本人权建设与推动、心理支持与辅导、感染者关怀与救治等综合服务。<br />
