五、政策建议<br />
<br />
（一）消除对维吾尔群众的污名化和政治恐惧<br />
<br />
对维吾尔群众的污名化和歧视无益于民族和谐和社会稳定。然而，我们也清晰地发现，很多污名化和歧视除了根源于不良社会舆论外，更根源于公职部门对维 吾尔群众在很多场合的过于“特殊化对待”，比如上文提到的：间接性的禁止居留、排斥就业以及“重大活动”前的排斥行动。因此，污名化的消除必须从公职部门 开始，从公职部门开始树立民族平等的工作作风和社会风气。在此基础上消除针对维吾尔群众的政治恐惧，重新认识到社会人口流动是社会发展的必然，维吾尔群众 在城市中生存和发展是社会多元和谐的真实写照。<br />
<br />
与此同时，相关职能部门应该扶持少数民族民间组织的发展。为少数民族群众在本民族社群中开展工作提供便利和优惠条件。<br />
<br />
（二）切实保障维吾尔群众的平等权利<br />
<br />
民族平等是指：各民族不论人口多少，经济社会发展程度高低，风俗习惯和宗教信仰异同，都是中华民族的一部分，具有同等的地位，在国家和社会生活的一 切方面，依法享有相同的权利，履行相同的义务，反对一切形式的民族压迫和民族歧视。。《中华人民共和国宪法》规定：“中华人民共和国各民族一律平等。国家 保障各少数民族的合法权利和利益，维护和发展各民族的平等、团结、互助关系。禁止对任何民族的歧视和压迫”。中国各民族公民广泛地享有宪法和法律赋予公民 的各项平等权利。诸如：各民族公民不分民族、种族、宗教信仰，都同样地享有选举权和被选举权；各民族公民的人身自由和人格尊严不受侵犯；各民族公民都有宗 教信仰自由的权利；各民族公民都有接受教育的权利；各民族公民都有使用和发展本民族语言文字的权利；各民族公民都有言论、出版、集会、结社、游行、示威的 自由；各民族公民都有从事科学研究、文学艺术创作和其他文化活动的权利；各民族公民都有劳动、休息和丧失劳动能力时从国家和社会获得物质帮助的权利；各民 族公民都有对国家机关和国家工作人员提出批评和建议的权利；各民族公民都有保持或改革自己风俗习惯的自由等等。[15]<br />
<br />
在维吾尔群众被严重边缘化的社会现实面前，要保障实质平等能够惠及每一个维吾尔同胞，就需要在政策和制度上能够保障对少数民族的扶持政策。切实地落 实国家的民族平等和民族团结的方针政策。为此，须对国家各级处理民族事务的机关进行必要的工作评估并建立相关机制保证各级机关能够更为积极地落实国家的民 族政策。<br />
<br />
（三）促进媒体改造<br />
<br />
为了有效促进媒体的客观公正报道，通过媒体消除社会舆论中对维吾尔群众的污名化和歧视，必须自上而下的实现媒体在此领域的改造。台湾人权促进会在谈 到媒体改造问题时指出：新闻报导应该秉持客观、中立、专业的原则：不报导未经证实的消息。不使用具歧视性或情绪性的字眼。新闻报导不应商业化、广告化；不 应沦为政治人物或政党的代言工具。[16]<br />
<br />
为了在有关维吾尔人的报道中实现这些目标，应该加强独立的媒体观察和媒体评估工作；政府应该支持学界对媒体的观察研究，提出消除媒体歧视的对策；在 民族院校建立少数民族群体新闻研究的项目。编写媒体培训手册。<br />
