（四）在居住领域的歧视和排斥问题的法律分析<br />
<br />
2002年11月27日国家民委、国家经贸委、公安部、国家旅游局、国家工商行政管理总局联合发出的《关于纠正极少数宾馆饭店旅店拒绝少数民族人员 入住行为的通知》中指出：“内地极少数城市的宾馆、饭店、旅店拒绝新疆籍人员，尤其是新疆少数民族人员入住。这种做法违反了党和国家的民族政策，损害了民 族团结，侵害了少数民族的正当权益，引起部分少数民族的强烈不满。”同时指出：“请你们[12]接此通知后，对本地区各宾馆、饭店、旅店负责人和员工进行 马克思主义民族观、党和国家的民族政策、法律法规及民族团结的宣传教育，同时采取有力措施，坚持纠正极少数宾馆、饭店、旅店拒绝少数民族人员或某地居民入 住的行为。只要入住的人员具备有效身份证件，遵守有关规章制度，都要一视同仁，给予热情接待，并注意尊重少数民族风俗习惯和宗教信仰，尽可能提供生活方 便，不能因其民族成份、居住地区的不同而区别对待。”<br />
<br />
2008年5月国办发〔2008〕33号文件：《关于严格执行党和国家民族政策有关问题的通知》指出：“在近期维护藏区稳定和加强反恐怖工作中，有 少数单位在工作中发生违反民族政策的行为，有的机场在安检保卫工作中以民族作为划分对象，有的出租车、宾馆、商店等出现拒载、拒住、拒卖等侵害少数民族群 众正当权益的现象，这些做法伤害了少数民族群众的感情，引起少数民族群众的不满。这些问题虽然发生在少数地区和单位，但是如果任其发展，将严重损害民族团 结，影响社会稳定，必须引起高度重视，坚决予以纠正，杜绝类似事情的发生。”同时指出：“各类宾馆、商店、餐饮等单位不得拒绝少数民族群众入住、购物、饮 食；各类交通工具经营者不得拒载少数民族群众。”<br />
<br />
从以上规定都可以看出，租房者和旅店拒绝维族群众入住本质上都是违背民族团结、民族平等的基本原则的，同时也是对国家法律的违背。<br />
