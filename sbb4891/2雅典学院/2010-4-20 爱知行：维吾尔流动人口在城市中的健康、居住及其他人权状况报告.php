前 言<br />
<br />
中国是一个统一的多民族国家，宪法规定：中华人民共和国各民族一律平等。国家保障各少数民族的合法的权利和利益，维护和发展各民族的平等、团结、互 助关系。禁止对任何民族的歧视和压迫，禁止破坏民族团结和制造民族分裂的行为。国家根据各少数民族的特点和需要，帮助各少数民族地区加速经济和文化的发 展。[①]然而，宪法的规定往往是原则性和宣言性的，在具体规定上远远没有实现宪法应具有的，对人的生存权、自由权、人的尊严和诉权等权利[②]的保障。 除宪法而外，中国对于民族问题还有《民族区域自治法》及其他的诸多法律文件进行规制。然而法律的良好规定却没有在实际上对民族问题的解决提供有利的制度保 障，一系列的民族冲突和民族矛盾不得不让我们重新审视现有民族政策所存在的问题。<br />
<br />
与此同时，随着城乡二元分割和贫富两极分化程度的加剧，很多少数民族地方的发展被边缘化，少数民族群众的生存、发展已成为日益严峻的问题。我们也看 到，很多边疆少数民族群众来到城市。由于文化、语言、信仰等原因，他们大多聚居在一起形成城市中小的聚落，在北京的新疆维吾尔聚落就是一个典型的例子。对 于这些小的聚落和其可能对城市造成的影响，有以下几个方面是首先需要在此报告中给与澄清的：<br />
<br />
这些人为什么不在家待着，而要到城市来？ <br />
这个问题本身或许即是一个伪命题。因为随着社会的发展，人员的流动已成为一个不可改变的事实。一般情况下，当一个人走在街上，你不会去考虑他是否 是外地人，或者他为什么要来城市。每个人来到城市都有他自己的理由。况且，当来到城市的理由是为了生存的时候，我们更应该反思我们提出的问题可能会对他们 造成的影响。<br />
<br />
他们有的会从事犯罪活动？ <br />
以在北京的新疆聚落为例，从媒体报道来看，确实有盗窃、吸毒甚至贩卖毒品的案例发生。[③]对于违法犯罪活动，任何公民都应该受到法律的制裁。但 是少数人的犯罪事实并不能说明所有人的情况，而且也不能因为个别案例案例而对整个聚落和民族以污名化。更何况，很多从事盗窃、贩卖毒品的青少年都是被拐骗 或胁迫而来到城市的。而且现代社会学研究的成果也进一步证明：犯罪的发生在很大程度上是与社会变迁、社会发展断裂以及人们生活的变迁是有着极大关系的，并 非某个人天生就有犯罪的基因。[④]<br />
<br />
为什么要关注他们的权利？为什么要给与他们保障？ <br />
《世界人权宣言》指出：人人生而自由，在尊严和权利上一律平等。他们赋有理性和良心,并应以兄弟关系的精神相对待。人人有资格享有本宣言所载的一 切权利和自由，不分种族、肤色、性别、语言、宗教、政治或其他见解、国籍或社会出身、财产、出生或其他身分等任何区别。<br />
<br />
《经济、社会和文化权利国际公约》[⑤]第六条一指出：本公约缔约各国承认工作权，包括人人应有机会凭其自由选择和接受的工作来谋生的权利，并将 采取适当步骤来保障这一权利。第十一条一指出：本公约缔约各国承认人人有权为他自己和家庭获得相当的生活水准，包括足够的食物、衣著和住房，并能不断改进 生活条件。各缔约国将采取适当的步骤保证实现这一权利，并承认为此而实行基于自愿同意的国际合作的重要性。第十三条一指出：本公约缔约各国承认，人人有受 教育的权利。它们同意，教育应鼓励人的个性和尊严的充份发展，加强对人权和基本自由的尊重，并应使所有的人能有效地参加自由社会，促进各民族之间和各种 族、人种或宗教团体之间的了解、容忍和友谊，和促进联合国维护和平的各项活动。<br />
<br />
《消除一切形式歧视公约》[⑥]第二条一指出：缔约国谴责种族歧视并承诺立即以一切适当方法实行消除一切形式种族歧视与促进所有种族间的谅解的政 策，又为此目的：（子）缔约国承诺不对人、人群或机关实施种族歧视行为或习例，并确保所有全国性及地方性的公共当局及公共机关均遵守此项义务行事；（丑） 缔约国承诺对任何人或组织所施行的种族歧视不予提倡、维护或赞助；（寅）缔约国应采取有效措施对政府及全国性与地方性的政策加以检查，并对任何法律规章足 以造成或持续不论存在于何地的种族歧视者，予以修正、废止或宣告无效；（卯）缔约国应以一切适当方法，包括依情况需要制定法律，禁止并终止任何人、任何团 体或任何组织所施行的种族歧视；（辰）缔约国承诺于适当情形下鼓励种族混合主义的多种族组织与运动，以及其他消除种族璧垒的方法，并劝阻有加深种族分野趋 向的任何事物。<br />
<br />
《中华人民共和国宪法》规定：中华人民共和国公民在法律面前一律平等。国家尊重和保障人权。任何公民享有宪法和法律规定的权利，同时必须履行宪法 和法律规定的义务。禁止非法拘禁和以其他方法非法剥夺或者限制公民的人身自由，禁止非法搜查公民的身体。中华人民共和国公民的人格尊严不受侵犯。禁止用任 何方法对公民进行侮辱、诽谤和诬告陷害。保护人民健康。<br />
