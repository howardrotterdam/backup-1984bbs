（四）就业<br />
<br />
l 法律规定<br />
<br />
《中华人民共和国劳动法》第十二条规定：劳动者就业，不因民族、种族、性别、宗教信仰不同而受歧视。<br />
<br />
《中华人民共和国就业促进法》第三条规定：劳动者依法享有平等就业和自主择业的权利。劳动者就业，不因民族、种族、性别、宗教信仰等不同而受歧视。 第二十八条规定：各民族劳动者享有平等的劳动权利。用人单位招用人员，应当依法对少数民族劳动者给予适当照顾。<br />
<br />
2008年5月国办发〔2008〕33号文件：《关于严格执行党和国家民族政策有关问题的通知》指出：生产经营者和服务提供者必须严格遵守法律法规 有关保障民族平等的规定，在招收员工时，不得歧视少数民族群众<br />
