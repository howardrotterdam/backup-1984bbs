北京爱知行研究所维吾尔项目：和谐族群的一盏微光<br />
<br />
作者：章剑锋 来源：南风窗<br />
<br />
<br />
<br />
12月7日的一次外展工作，25岁的维族小伙艾克被弄得有点狼狈，从中不难看出，“底层关怀”理念要被付诸实践，是多么不容易的一件事情，特别是在 试图抚平族群问题的时候。<br />
<br />
愤怒的房子<br />
<br />
在大兴一个维、汉民聚居区，艾克试图说服一些汉民房主将房子租给维民居住，屡屡吃了闭门羹。<br />
<br />
“我们又不是狼！你不用赶我，我不到你房子里去，”40来岁的维族女士美丽开难掩激动，声音颤抖着，“房子是你的，不是我们的。我要是干了坏 事，你可以把我赶出去……你给，我们就住，不给，我们就不住。又不是小工，又不是狼，你叫我们走走走，啥意思啊？”<br />
<br />
艾克带她去看了一所比较整洁的单间屋子，房主儿子本来在电话里一口答应可以出租给新疆人，但房主本人态度决绝，头摇得像个拨浪鼓，“咣”一声将 门锁上，闪身躲避。<br />
<br />
这个聚居区里，聚集了100多名新疆人。在今年10月之前，他们全部租不到房子，天一擦黑，就钻入网吧、浴池等公共场所呆上一晚。两个月前，艾 克他们进入这里，帮忙找房子，一家一家敲门进去和房主沟通。就这样，好不容易解决了30多人的居住问题。艾克算了一下，30多人一帮一，就可以安置下60 多人。<br />
<br />
12月7日中午，一位房东找到艾克，投诉刚搬进去一周的维族房客。艾克这才知道，自己介绍进去的原房客未经允许私下将房子转租易手，现在里面住 着一个腿脚有问题的年轻维吾尔人，语言不通。住进去之后，又招来三四个维族男子同宿。一群人不讲卫生，并且深更半夜在院里劈柴，把房东吓坏了，一边诉说一 边落泪。<br />
<br />
艾克勃然大怒，把几个蓬头垢面的维族男子全部轰了出来，一边提出增加租金，另找一对维族夫妇接替，一边赔不是宽慰房东，“嫂子，你别哭，有问题 了，不值得为他们掉眼泪。我们穆斯林讲究干净整洁、真善美，我们也痛恨这样的人。”<br />
<br />
艾克不是房屋中介，而是在京维吾尔人项目的工作人员。这个项目，由北京爱知行研究所设立，负责人是防治艾滋病社会活动人士万延海。项目旨在帮助 维吾尔流动人口获得健康教育和基本医疗服务。两项主要的目标，是要使艾滋病感染者获得免费医疗、检测服务，并帮助吸毒者得到政府提供的美沙酮维持治疗。<br />
<br />
要达成这些目标，必须确保流动维吾尔人更好地融入所在城市和社区，推动其他民族的人认识他们的生存和健康状况。这类工作有其艰巨性。在大兴那个 庞大的城乡结合部，维、汉民关系一目了然。这些流离失所的维民，如能安顿下来，维吾尔人项目将能对他们进行相应教育和干预，比如美沙酮和一些免费检测，根 据当局的规定，只有固定居所并办理了暂住证的人才可得到。这些事务，就都落在了艾克等人身上。<br />
<br />
“有时候一天见30个房东，谈成的只有一两个。”艾克说。更不顺利的时候，见了70个房东，一个也不成，连交谈机会都没有，就被轰了出来。还要 表现得彬彬有礼，他认为，这在一定程度上有助于改善外人对维吾尔人的负面印象。<br />
<br />
“这两个月搞得太累了，”毕业于中国人民大学的艾克，把找到房子看作是一项挑战，“每次能帮助一个人住进房子，成就感是非常大的。”<br />
<br />
底层的维族人，过着近似游牧一样的城市生活。早前，北京曾出现过魏公村、甘家口等维吾尔族聚居地，两地先后被驱散了，他们就向更为僻远的大兴集 中。这些人当中，有摆小摊儿的、有开小餐馆的、也有做新疆土特产贩卖生意的。美丽开原来就在甘家口开餐厅，现在变得不再富有，一身疾病，连个居所都不稳 定。“北京的人，就是不理解我们……”<br />
<br />
在大兴聚居区的维民中，有五六位艾滋病感染者。早些时候，一位感染者去世，直接影响到那一地点的其他维吾尔人。房东陆续解除了租房合同，这种恐 慌最终演变成各种误解，将新疆人推向更为边缘化的境地。有些严重的病人，实在没有办法，只好回到新疆去。<br />
<br />
项目办为此发出严厉警告，“在京维吾尔人所面临的健康、公共卫生等问题已经非常严峻，限制和排斥该群体的最基本生存和居住权利，是不应该的。”<br />
<br />
<br />
<br />
突发事态<br />
<br />
2006年，有人建议万延海到北京西站地区的维吾尔人聚居区看一看。当时他了解到，那些地点注射用针头遍地，连小孩儿都在玩针头。随后，他们向全球基金和 德国米索尔基金会申请资助，设立了维吾尔人项目。这两个组织，都有针对少数民族群体的健康干预专项资助。<br />
<br />
“也挺害怕的，因为完全陌生，不了解，”万延海说，“能躲的也想躲。我们的更多精力，是做能做的、熟悉的和安全的事情。”<br />
<br />
维族项目已经在北京和昆明两地开展工作，他们从中央民族大学等一些高校招募维族学生担当志愿者，并将这些人派往各个维族流动人口聚集场所，通过联 谊、咨询和培训等外展工作，向维吾尔群体提供健康教育和医疗转介服务（免费检测、清洁注射器交换、美沙酮治疗等），也通过与官方进行沟通和交涉，帮助他们 取得某些基本的公民待遇。就项目本身来说，他们也会对维吾尔人进行一些物质帮扶，比如给予贫困者基本生活资助、协助无力承担丧葬费用的感染者家属处理善后 事宜等。<br />
<br />
维吾尔项目原来有7个专职工作人员，都是高校毕业生，每月拿着1000多元生活补助，不长时间就陆续离开了。现在的3个维族青年，像艾克这样， 有的已经坚持了两年多。如果不遇到重大变故，工作进展还算顺利，但遇到一些重大节庆或事件，很多工作会陷于停顿。<br />
<br />
“我们的能力太有限了，”艾孜提说，“社群里有各方面的需求。”<br />
<br />
艾孜提是中央民族大学毕业的学生，早些时候，爱知行与一些高校社团建立过合作关系，也资助过中央民大的一些学生赴新疆支教。毕业了，她就向爱知 行求取了这个职位，担任项目专职人员。但像找房子那样一波三折的工作，并非女孩子能够胜任，于是就都交给艾克一个人天天去跑。<br />
<br />
维吾尔流动人口的不固定和周围环境的变动，给项目工作带来了难度。好不容易发展起来的社群志愿者，可能由于一些意外情况而从此失去联络，此时， 社群联系就会出现断档，需要重新培养。他们所关怀的，始终是不断切换着的陌生面孔。<br />
<br />
“我们不能保证永远做下去。对于政府来说，我们做的也许是多余的工作，本来这就是他们管的事情，”艾孜提的同事、南京大学毕业的女孩热依莱说， “如果政府尽职尽责，我们就没必要去做了。”<br />
<br />
发生在新疆的一系列暴力事件，加剧了维吾尔人的民族污名化和社会歧视处境。如果没有专门针对维族流动人口的第三方帮扶力量出现，这些维吾尔流动 人口，只怕真会成为美丽开所说的那样，是“睁着眼睛的瞎子”。相应地，对艾克们的到来，他们寄予了过于沉重的期望，有些事情，完全是艾克等人力所不能及 的。<br />
<br />
这个地点据称已经写入政府的规划，最迟在明年春天就会夷为平地，居民们需要迁离，而那些没有房子的维吾尔人，又要走上一条未知之路。在此之前， 这个始终被排斥在主流视线之外的社会，将一直保持着它的本来面貌。<br />
<br />
12月7日午后，聚居区内冒出一个四处找白粉的汉民男子。这个瘸着一条腿、口齿不清的男人，在扫完一条街后，一无所获。接着进到一家维民开的小 卖部里。当时艾克正和店主夫妇交谈。听说要找白粉，店主和艾克同时厉声警告此人离开。“赶快出去，别影响人家做生意，”艾克说完，转而向本刊记者叹气， “这又要引起误解了。”<br />
<br />
傍晚时分，聚居区里还发生了一起维、汉居民之间的暴力冲突。一群汉民举着棍棒和长长的大砍刀，把手里端着一把菜刀的一个维族男子逼进胡同里。人 越聚越多，对峙一阵后，又各自散开了。这功夫，艾克大声喝斥了一些维吾尔人，一位妇女和他申辩了几句，艾克挥挥手，示意她赶紧离开。<br />
<br />
这件事情让一直小心翼翼处理社群事务的艾克感到忧虑。冲突可能激化矛盾，加深误解，使他们的帮扶工作前功尽弃，维吾尔人将更难从汉人那儿租到房 子住。但他又不无侥幸，觉得事情发生时正好天色已晚，事态也没有扩大到整个聚居区。<br />
<br />
天黑了，奔忙了一天，艾克准备返回市里。那个被围攻的维族男子找来，向艾克说明刚才的冲突原委。他的脖颈处有两条被砍开的伤口，正在渗血，皮衣 的左肩处，也被砍出两条长长的刀痕。艾克要求他控制一下自己的情绪，先去诊所包扎伤口，什么都别想。他答应明天过来再协调此事。一路上，他为两个月以来好 不容易维持住的平静局面被打破而遗憾。<br />
<br />
摆在维吾尔项目面前的，是更为现实和纠结的族群矛盾，他们需要在一个有限的范围内妥善求解这个难题，绕不开，也回避不了。这决定了项目本身―― 一盏点了3年的微光，能否变得更明亮一些。<br />
<br />
