<br />
l 事实状况<br />
<br />
在北京郊区某维族人聚居地社区，大约有10多个幼龄儿童，其中也有部分儿童达到入学年龄。但因为入学条件高、家长无法承担高额的费用，以及部分儿童 因为“新疆人”的身份无法正常获得入学接受教育的权利。<br />
<br />
（六）“重大活动”前经常被排斥的事实<br />
