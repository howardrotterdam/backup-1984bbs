一、北京流动维吾尔人群的基本情况<br />
<br />
在整个维吾尔族人口中，城镇人口有163.31万人，占总人口的19.44％；乡村人口676.63万人，占总人口的80.56%。[⑦]加之很多 地方自然条件恶劣、经济发展水平较低，造成了农村富余劳动力大量增加，同时贫困现象日益严重。随着改革开放程度的加深，越来越多的维吾尔群众来到北京、上 海及其他内地城市。维吾尔人到内地刚开始主要经营餐饮业，之后内地很多省市相继出现了不少维吾尔商人人经营的餐厅，羊肉串摊位，在内地很多省市经营水果， 布料，毛皮，棉花生意的维吾尔商人也不少。现如今内地经商，开办自己的企业（主要经营餐饮，新疆土特产，毛皮，国际国内贸易等等）的维吾尔人越来越多。据 了解光是在北京维吾尔人经营的餐厅不少于50多家。[⑧]另外，还有部分人从事盗窃、零星贩毒等违法活动。<br />
<br />
由于文化、语言、信仰等因素，维吾尔人在北京主要是同一民族的群众聚居在一起。早期主要居住在魏公村一带，后来由于一系列重大事件（比如奥运会在北 京的举办），维吾尔人在政府干预之下先后聚居于甘家口、西客站、大兴等地，居住也呈现出日益分散的特点。<br />
<br />
总的来说，在京的维吾尔人有以下特点存在：<br />
<br />
（一）文化适应的问题<br />
<br />
1.从维族聚居区来到汉族聚居区，身份认同上的差异导致交流中的隔阂和心理压力；[⑨]<br />
<br />
2.在汉族社区，与伊斯兰教信仰密切关联的生活习俗的进行有很大的障碍。特别是当礼拜与工作时间相冲突以及场所无法得到保证的时候更是如此<br />
<br />
3.维族人与汉人间缺乏沟通、交流并存在某种程度的误解和歧视。维吾尔族与汉族生活在同一个城市，却各自生活在自己的圈子里，哪怕是在饭馆里，也很 少能够看到汉族人的身影。在和一些出租车司机的交流中我们了解到一些当地人对于维吾尔族的看法，很多人不喜欢甚至厌恶维吾尔族，将他们与小偷划上等号，认 为他们野蛮、不讲道理。而所有这些做出这些评论的人，没有一个真正与哪怕一个维吾尔族人交流过；而在另一方面，维吾尔族人对于主流社会这种看法也相当的不 满意，尤其是汉人对他们普遍存在歧视。<br />
<br />
（二）受教育水平低<br />
<br />
受教育程度低是维吾尔人群的普遍共性。这一特点影响制约着这类人群的生活状态。我们了解到社群中没上过学的大有人在，大部分人小学未毕业，仅有一小 部分人员只完成了小学学业、极个别的人有大中专学历。很多人不会说汉语、听不懂汉语，大部分人甚至连维吾尔语也不会写、更是看不懂维文。<br />
<br />
（三）居住条件差<br />
<br />
目前京区处于社会较为边缘的维吾尔人口数量已经明显减少。他们零星聚集在京周边区域，每一小点聚集人数不一，有二三十人聚集的、有六七十人聚集的。 选择这些较偏的地方，一是因为房租相对市中便宜，二是就目前新疆人租房困难，在这些偏远城郊地区，他们租房困难的情况稍有缓解。筒子楼、平房，居住房租大 都破旧、室内空间较小。他们本身没有什么经济来源，加上租房困难，更多的人都是相近的几个朋友、或者一家亲戚好几个人，居住在同一个房间里。<br />
<br />
（四）人员流动性大<br />
<br />
2008年奥运前，北京加大了对流动人口的管理力度；酒店、旅馆限制“新疆人”入住，以前给租房的房东们拒绝再继续为这些维吾尔人租房；加上警方对 吸毒人员及贩卖毒品人员的严厉抓捕；等这些原因，在城市较繁华区域很难见到这些成片的维吾尔人。他们大多流向内地其他大城市谋生，剩余的这些人逐渐远离市 中，搬到郊区等偏远区域。<br />
<br />
（五）健康状况堪忧<br />
<br />
很多维族流动人口没有正当工作、没有固定经济来源。他们中的一些人为毒品使用人员，以贩养吸成为他们直接获得自己所需毒品的来源和生活经济来源；另 一部分人，自己不吸毒，但因为家属中有成瘾者，不得不靠贩卖毒品，获得毒资及金钱，来养家活口，这类人中女性居多，也有部分未成年儿童。毒品使用给公共安 全造成了极大地威胁。在维族社群中，艾滋病传染率较高。艾滋病、结核、肝炎，成为这一人群人常见的病情。<br />
<br />
（六）儿童和青少年成长问题突出<br />
<br />
前几年，内地出现很多新疆维吾尔未成年青少年、儿童等，在街道扒窃、成为职业小偷。而且这些儿童和青少年很多都是被诱骗到北京等城市，然后被强迫从 事盗窃等违法活动。现如今，这些当年的孩子已经成人，大都20岁左右。他们成为一批年轻的吸毒、贩毒人员，偶尔仍在进行偷窃。<br />
