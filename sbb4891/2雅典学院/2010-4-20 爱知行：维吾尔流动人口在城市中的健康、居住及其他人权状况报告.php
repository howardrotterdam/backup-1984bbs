三、边缘化与维吾尔流动人群的健康危机<br />
<br />
在北京的维族社群中，很多人员都没有正当工作、没有固定经济来源。他们中的一些人为毒品使用人员，以贩养吸成为他们直接获得自己所需毒品的来源和生 活经济来源。毒品使用给公共安全造成了极大地威胁。在维族社群中，艾滋病传染率较高。艾滋病、结核、肝炎，成为这一人群人常见的病情。同时，由于居住条件 恶劣，流感等传染性疾病也会在社群中时有爆发。<br />
<br />
（一）艾滋病的威胁<br />
<br />
1.社群中的艾滋病流行状况：流动维族成瘾者中艾滋病感染者人群，由于被感染时间长、流动性强、居住不稳定、生活极度贫困等因素的影响，绝大多数艾 滋病感染者处于发病期，但是由于地域限制，“四免一关怀”等政策难以惠及这一部分人群。部分艾滋病感染者由于缺乏有效的医疗支持，时有病死于街头的个例发 生。<br />
<br />
2.认知水平较低：很多人知道艾滋病在威胁的他们的生命，也知道目前为止没有可以治愈艾滋病的药物，但是对于传播途径、预防措施、抗病毒治疗等方面 的知识很不清楚。他们中的一些人知道美沙酮替代治疗，也知道针具交换，但并不了解相关知识。谈及这些方面，他们会说，社群有人服用美沙酮突然死亡、甚至有 人已经发疯，所以社区很多IDU人员不敢尝试服用此药。<br />
<br />
3.政策因素及其对治疗可及性的影响：艾滋、结核等病人，获得抗病毒药物及治疗受限。相关政策规定，使这些病人无法在京获得药物，必须在原籍卫生部 门获得相应的药物。而这些病人中的大多数，同时又为毒品使用人员，也是靠毒品交易为生，缺乏对疾病的正确认识和相关政策的了解，本身经济状况较差，生病无 法及时接受治疗，获得抗病毒药物更是受地域和户籍限制。<br />
<br />
4.减低危害措施很难惠及维族人群：一方面，很多社群人员因为没有固定住址，而无法办理暂住证，进而无法获得美沙酮维持治疗；另一方面，由于目标群 体宗教和文化敏感性，对于艾滋病、性病、以及安全套等相关方面的内容比较排斥。提供给目标群体的安全套是一种被动接受方式发放的。<br />
<br />
5.艾滋病与其他传染性疾病并发现象普遍：在维族社群中，因为艾滋病与结核双重感染得不到及时治疗而病死于街头的现象时有发生。同时，由于不能得到 及时的抗病毒治疗，其他很多疾病与艾滋病同时并发的现象在社群中也很普遍。<br />
<br />
（二）其他疾病的威胁<br />
<br />
1. 其他传染性疾病的传播：住房困难和居住环境的恶劣加剧了流感、呼吸道等传染性疾病在维族流动人员以及居住在社区、网吧、桑拿房等公共场所的人中的传播。根 据爱知行研究所维族项目官员对社群走访调查发现：在冬天大部分社群人士都未能避免严冬流感问题，还有相当数量艾滋感染者及病人、结核病人、年纪较大切体弱 多病的人士以及10多个儿童都面临严重的健康问题。几乎所有的社群人士都面临不同程度的流感等疾病。<br />
<br />
2. 因社群文化差异、宗教思想影响等原因，在预防艾滋、性病的方面安全套的推广始终有一定程度的困难。大多数人没有普遍使用安全套的习惯，对其也没有科学的认 识。这直接导致维族社群中青年男女易患性病等疾病。<br />
<br />
（三）民族政策中对少数民族医疗的规定<br />
<br />
《城市民族工作条例》[13]第二十一条规定：“少数民族人口较多的城市的人民政府，应当根据实际需要和条件，建立民族医院、民族医药学研究机构， 发展少数民族传统医药科学”。<br />
<br />
2008年5月国办发〔2008〕33号文件：《关于严格执行党和国家民族政策有关问题的通知》指出：“各类医院对少数民族患者要一视同仁，不得歧 视。地方各级人民政府和政府各部门要依照各自职责对生产经营者和服务提供者执行民族平等政策的情况进行检查。对违反有关法律法规的，要坚决予以纠正，依法 严肃处理，情节严重的，要依法追究刑事责任。”<br />
