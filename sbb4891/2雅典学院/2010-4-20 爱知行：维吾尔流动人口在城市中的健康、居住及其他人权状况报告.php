四、在京维吾尔族流动人群面临的其他人权和法律问题<br />
<br />
（一）语言障碍<br />
<br />
l 中国的法律规定<br />
<br />
-《中华人民共和国宪法》第一百三十四条规定：各民族公民都有用本民族语言文字进行诉讼的权利。人民法院和人民检察院对于不通晓当地通用的语言文字 的诉讼参与人，应当为他们翻译。在少数民族聚居或者多民族共同居住的地区，应当用当地通用的语言进行审理；起诉书、判决书、布告和其他文书应当根据实际需 要使用当地通用的一种或者几种文字。<br />
<br />
-《中华人民共和国人民法院组织法》第六条规定：各民族公民都有用本民族语言文字进行诉讼的权利。人民法院对于不通晓当地通用的语言文字的当事人， 应当为他们翻译。在少数民族聚居或者多民族杂居的地区，人民法院应当用当地通用的语言进行审讯，用当地通用的文字发布判决书、布告和其他文件。<br />
<br />
-《中华人民共和国刑事诉讼法》第九条规定：各民族公民都有用本民族语言文字进行诉讼的权利。人民法院、人民检察院和公安机关对于不通晓当地通用的 语言文字的诉讼参与人，应当为他们翻译。在少数民族聚居或者多民族杂居的地区，应当用当地通用的语言进行审讯，用当地通用的文字发布判决书、布告和其他文 件。<br />
<br />
-《中华人民共和国行政诉讼法》第八条规定：各民族公民都有用本民族语言、文字进行行政诉讼的权利。在少数民族聚居或者多民族共同居住的地区，人民 法院应当用当地民族通用的语言、文字进行审理和发布法律文书。人民法院应当对不通晓当地民族通用的语言、文字的诉讼参与人提供翻译。<br />
<br />
-《中华人民共和国民事诉讼法》第十一条规定：各民族公民都有用本民族语言、文字进行民事诉讼的权利。在少数民族聚居或者多民族共同居住的地区，人 民法院应当用当地民族通用的语言、文字进行审理和发布法律文书。人民法院应当对不通晓当地民族通用的语言、文字的诉讼参与人提供翻译。<br />
<br />
-《城市民族工作条例》第二十条规定：城市人民政府应当保障少数民族使用本民族语言文字的权利，并根据需要和条件；按照国家有关规定加强少数民族文 字的翻译、出版和教学研究。<br />
<br />
l 事实状况<br />
<br />
-在接受治疗的时候：社群人员在京卫生部门、疾控等地，进行检测咨询或者抗病毒药、美沙酮转接等问题时，都会遇见语言障碍。由于语言障碍，使得很多 人很多后续问题无法跟进。比如：社群较集中的几个美沙酮门诊，社群人士即是解决户籍限制问题可以进行转介，但却因语言交流困难，无法跟进后续问题、以及平 日的活动、知识的获得等等，出现服务中断等问题。<br />
<br />
-在儿童受教育的情况下：在维吾尔居民居住较多的社区没有相应的维吾尔语言学校。面临使用全汉语学习的障碍。<br />
<br />
（二）办理护照、暂住证等证照时候的不便状况<br />
<br />
l 中国的法律规定<br />
<br />
2008年5月国办发〔2008〕33号文件：《关于严格执行党和国家民族政策有关问题的通知》指出：在履行发放许可证书、开展执法检查、实施行政 处罚、采取强制措施等职责中，尤其是在车站、机场、码头、出入境等安全检查中，不得歧视少数民族群众，不得有影响民族关系的言行，一经发现违法情况，要立 即纠正，严肃查处。<br />
<br />
l 事实状况<br />
<br />
几乎所有该社区人士都没有办法办理暂住证，有些人遭到拒绝的原因：新疆人不予办理暂住证。并且还有一部分在京从事汽车、商品交易的生意人因没有暂住 证，生意相关手续及证件都受到限制，遇到严重困难。依照《中华人民共和国户口登记条例》、《北京市外地来京务工经商人员管理条例》、《北京市外地来京人员 户籍管理规定》、《北京市外地来京人员租赁房屋治安管理 规定》等法律、法规、规章、规范性文件，如果没有暂住证，流动人口将不能租房，不能报考。甚至有可能会被罚款或者暂扣身份证。[14]同时，在七五事件 后，很多新疆籍公民在办理护照、办理工商执照等方面都会受到各种各样的限制。 <br />
<br />
（三）宗教礼拜<br />
<br />
l 法律规定<br />
<br />
《城市民族工作条例》第二十四条规定：城市人民政府应当保障少数民族保持或者改革民族风俗习惯的自由。第二十六条规定：少数民族职工参加本民族重大 节日活动，可以按照国家有关规定放假，并照发工资。<br />
<br />
l 实施状况<br />
<br />
据社群人士介绍：在劳教所、强制戒毒所等地方，应该考虑穆斯林饮食的照顾政策。具体来看，清真食品供应的标准不应只是有无猪肉，而应该建立专门的穆 斯林餐饮场所。在关押期间的监管场所，应尊重少数民族的礼拜仪式，提供礼拜基本条件。 <br />
