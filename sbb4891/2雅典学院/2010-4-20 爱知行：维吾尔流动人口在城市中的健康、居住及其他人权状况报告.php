<br />
二、在居住问题上的困境<br />
<br />
绝大多数维吾尔人因为民族语言、文化及宗教信仰等诸方面与汉人间的差异使他们不能很好地和汉人形成有效沟通。同时，社会舆论的偏见和歧视往往会加深 租房人对维吾尔人的刻板影响和歧视；再加之城中村的大规模拆迁和改造运动不断发生，使得大部分流动维族居民无房可租住，或者说即是有房子可租他们也很难租 到。与此同时，很多旅店宾馆拒绝向维吾尔人提供旅店服务，居住的问题同样影响住店的维吾尔人。因此，居住地问题已然成为2009年维吾尔社群民众最为关注 的民生问题之一。<br />
<br />
（一）居住条件恶劣及其对社会治安和社群健康的影响<br />
<br />
据美国学者Kalk的研究显示：在19世纪80年代至20世纪20年代的美国“进步时代”，政府给无家可归的人提供廉租房，但即使这样，也未能很好 的解决因住房不稳定而产生的治安和公共卫生问题。廉租房屋内恶劣的居住和卫生条件，加之过分拥挤，使之成为治安、犯罪等社会问题的集中区域。而贫民窟的恶 劣条件则是疾病流行的温床。例如，1899年纽约某一个街区居住了3 688人，其中241人患有肺结核，平均每1 000人中有超过1 /4的人死于肺结核。疾病的流行自然不会局限于贫困社区，必将威胁到整个城市。上流社会在将疾病流行归咎于居住在廉租社区的贫民的同时，也不得不关注城市 贫穷群体的住房问题。[⑩]<br />
<br />
以此为鉴，对于流动的维吾尔人，一味的高压和通过政府行为的排斥[11]可能对于社会治安稳定以及公共卫生问题的解决只会适得其反。<br />
<br />
（二）维吾尔人的现实困境1：很难租到房子<br />
<br />
维吾尔人由于文化上与汉人社群的巨大差异，导致他们很难融入到汉人社群中，这也就在一定程度上使得他们很多会以小聚居的形式居住在一起。<br />
<br />
然而，由于很多原因的存在，他们很难租到房子。据北京爱知行研究所的维族项目官员提供的走访记录显示，至少有以下原因影响到维族人租住住房时候的境 况：<br />
<br />
文化差异的原因：有社群人士表示，他们不是不想把房子租给维吾尔人，是怕有些人爱热闹，晚上打扰他们休息，还有由于语言的障碍，很难交流，生活习 惯不一样。 <br />
――从实际情况来看，这绝大多数时候都是一个借口。因为就现在维族人居住较为集中的大兴区的社区来看，大多是城郊的低矮平房区，很少有汉人、房东在此居 住。居住的除了维族人外基本都是租住的外地人。<br />
<br />
刻板印象：由于部分维吾尔人有过违法乱纪的事实，部分维吾尔人是警察的执法对象，警察时常来监督社区的治安情况，也经常询问部分维吾尔人的身份情 况。于是给社群群众造成了“维族人都是坏人”的刻板印象。 <br />
――结合前边的论述，维族社群里边确实有一部分人有过违法犯罪的记录，但是这不能代表所有人都是“坏人”。<br />
<br />
政府（公安部门）“打招呼”：由于“七五事件”等事件的爆发，使得各地公安机关把维族人视为维稳工作的重点关注对象。也正因为此，很多地方层面 的公安机关直接对维族人进入本地、本社区持“不欢迎”的态度。于是，他们会找到房东，交代不能把房子租给维族人。从爱知行研究所维族项目官员的访谈记录来 看，有很多房东都被当地公安“交代”过，所以他们也不敢轻易把房子租给维族人。 <br />
――以维稳为理由，通过政府行为直接对维族群众进行排斥和拒绝本身即是对法律规定的违反。交易自愿是合同法的一项基本原则，政府对公民间正常的合同关系进 行非法干预不仅超越职权，而且还烙上了严重的民族歧视的烙印。<br />
<br />
据爱知行研究所维族项目官员的调查显示，大兴区流动维吾尔人现在有100多人，60%以上的人没有租到房子。因为没有固定住所，其中有些住在了网 吧、桑拿、宾馆等高消费场所。<br />
<br />
（三）维吾尔人的现实困境2：在北京无法入住旅店<br />
<br />
北京爱知行研究所在北京市海淀区、石景山区、丰台区、宣武区２０家宾馆及招待所进行了调查。在这些宾馆中，只有８家宾馆表示可以接纳新疆维吾尔人入 住，也就是说，不允许的比例达到了６０％。而且，在这些说可以入住的宾馆中，只有某五星级大酒店的服务员不带丝毫犹豫地回答了我们的问题，其他的都做了认 真的考虑。另外一个重要信息是很多说可以入住的宾馆都要求新疆维吾尔族友人做好被查的准备。拒绝的主要原因也大多是因为有“上级的指示”。<br />
