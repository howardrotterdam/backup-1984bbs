附三： 给北京市公安局长的信：关于新疆籍维吾尔人在京居留、社会建设和卫生保健问题 <br />
<br />
马振川局长、先生： <br />
<br />
我叫万延海，北京市民，在民间组织从事艾滋病防治工作。<br />
<br />
今天下午，2009年1月11日，在志愿者的陪同下，我访问了北京大兴一个新疆维吾尔人聚集区，探访1名感染艾滋病病毒和发病的妇女。我们到达她家 里的时候，她骨瘦如柴，非常痛苦地躺在床上，并询问我们如何可以戒毒和治疗艾滋病。我们答应第二天带她去医院看病。<br />
<br />
不幸的是，今天晚上，传来消息，这个妇女已经去世。我对我们没有及时帮助这个妇女感到内疚。这是我们一周之内听到的第二个死去的新疆维吾尔族妇女。<br />
<br />
我不仅为这个妇女感到难过，也为我们这个城市没有很好对待来自新疆的维吾尔人民感到不解。这个妇女死在并不缺医少药的北京城里，死亡的时候却并无医 学照顾，我国政府提供给贫困艾滋病感染者的免费医疗政策并不覆盖这些流浪在我国各大城市边缘里的维吾尔族人民，而她们多半并不了解这个政策，也无法获得这 项政策的照顾。<br />
<br />
今天下午，就在我们访问这个地区的时候，我听到了一个我们已经听说很多次的消息：公安部门召集该聚集地的出租房屋的居民，要求他们不得出租房屋给新 疆人。<br />
<br />
在过去的10年中，在北京这个城市，我们目睹了新疆人聚集点从当年的魏公村迁移到甘家口、西客站、万泉寺和现在的各个小聚集点。<br />
<br />
我注意到，城市街头的新疆维吾尔人聚集区存在很多治安问题，非常头疼。但是，我坚持认为，处理新疆人聚集区里的治安问题、公共卫生问题和政治问题， 北京市政府包括公安部门首先需要尊重维吾尔人民的迁徙和居留的权利，而不能因为存在问题就随意驱赶，不能随意抓捕。相反，我建议，北京市政府部门可以投入 专项经费，帮助处于城市边缘环境中的少数族裔发展，帮助他们获得暂住证，开展社会就业培训和创造就业机会，让儿童全部在北京市入学而不是现在的几乎全部失 学，解释我国政府帮助艾滋病人的免费治疗政策，提供降低毒品伤害的卫生服务，让维吾尔人民融入到首都这个城市里，感受到国家对人权和人民健康的关爱和责 任。<br />
<br />
我注意到我们这个城市对维吾尔人存在下列歧视，一些歧视明显来自北京市公安部门的执法行动：<br />
<br />
1、旅店业普遍不接受新疆人（特别是维吾尔人）住店，有旅店2008年10月与我机构签署会议用旅店协议时明确说明指令来自公安部门，<br />
<br />
2、私人房屋不出租给新疆人，<br />
<br />
3、公安部门不给办理暂住证，<br />
<br />
4、公安部门在打击毒品的名义，对维吾尔人聚集区的人民随意抓捕和驱赶，而不是依据法律和证据，<br />
<br />
5、维吾尔人被渲染为首都政治稳定的威胁，虽然我们持续地关注到这个群体的不幸和遭受的伤害，包括在过去一周内死去的2个妇女艾滋病人，<br />
<br />
6、儿童无法入学，<br />
<br />
7、居无定所，生活条件非常简陋，<br />
<br />
8、缺乏基本的卫生教育和卫生服务，对我国政府艾滋病关怀政策不了解，<br />
<br />
9、无法获得我国政府提供给艾滋病人的免费治疗和关怀服务，<br />
<br />
10、无法获得我国政府提供给吸毒者的美沙酮替代治疗服务，<br />
<br />
11、缺乏安全的环境，不能持续和全面地获得清洁的针具，<br />
<br />
12、缺乏政府政策和经济支持，志愿者和社会组织需要冒着极大风险，包括被警察抓捕和感染疾病。<br />
<br />
<br />
<br />
鉴于上述情况，我强烈建议北京市政府特别是北京市公安局：<br />
<br />
1、停止任何针对维吾尔人的不友好行动，帮助维尔人民在北京城里安局乐业，<br />
<br />
2、出台政策和划拨资金，帮助维吾尔人社区发展，包括发展教育、发展社区社会组织、发展就业机会、发展医疗服务等，<br />
<br />
3、紧急支持社区社会组织发展，在维吾尔人民中开展全面的健康教育，提供健康服务，<br />
<br />
4、紧急划拨医疗救助经费，紧急安排医疗机构专门负责艾滋病感染者治疗和关怀，<br />
<br />
5、对维吾尔人中的吸毒者，提供戒毒服务和降低伤害的服务，<br />
<br />
