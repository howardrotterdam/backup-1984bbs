																											<div id="postmessage_466047" class="t_msgfont">二十三、一些说明<br />
<br />
很长时间以来，我一直在跟踪着征地和拆迁的演化和发展。我感到诧异的是，国人在这么重大的问题上缺乏权益的观念。像冰箱和电视机的质量问题，消费者的要求已经近乎苛刻，而对牵扯到巨大利益的拆迁问题，却很少有人去据理力争。对于拆迁中的问题，我写过很多文章。因此，本文不仅仅是针对《城市房屋拆迁管理条例》的修改。<br />
<br />
在本文中，谈到了很多与《城市房屋拆迁管理条例》没有直接关系的问题。我认为，这些与宪法和物权法精神有关的问题，是修改旧《条例》时不应该回避的。<br />
<br />
我要强调的是，必须注意新的《条例》的立法取向。我认为，正确的立法和执法取向应该是首先考虑公民的权利，维护公平交易，然后才是有关建设的问题。只有兼顾到具体的，一个一个公民的权益的公共利益才是真实的，否则就只能是在公共利益掩盖下的权贵的私利。<br />
<br />
最后，谈一谈本文有些观点的出处，或者在本文之前就已经有的各种观点。<br />
<br />
“争取实现《不动产征收法》”的说法，来自秋风的文章。<br />
<br />
在凤凰卫视的“一虎一席谈”中，邀请学者和教授，其中有秋风、王才亮、应松年、王锡锌、孙宪忠、马跃成、华新民、夏霖等讨论了《城市房屋拆迁管理条例》的修改问题。秋风认为，在征收中应该实行自愿交易优先原则。他还认为，征收的程序必须是民主的。王才亮认为，市场估价仅仅是征收补偿价格的起线，而不是唯一标准。更重要的，我们要实事求是地解决被征收户的各种困难。王锡锌同意王才亮的观点，他补充道，拆迁，实际上是一种非自愿移民，国际上要加倍补偿。&nbsp; &nbsp;孙宪忠认为，公共权力机关不能从这种土地运作的过程中赚钱。关于农村土地的征收问题，秋风主张修改《土地管理法》，保障农民的权益。征收农民的房屋，要同地同价。&nbsp;&nbsp;<br />
<br />
还要指出的是，俗士在《“搬迁条例”的实质是“征收”》中提出了警告，他说：<br />
<br />
对于“搬迁条例”，相关专家认为“新条例草案强调‘征收’和‘搬迁’都是为了‘公共利益’”，至于什么公共利益，专家们并没有明确。我想，按照我国的特色，一旦政府规划确定，那么这个规划就应是复合所谓“公共利益”的，于是乎，因之所必须的征收也就是为了实现“公共利益”。沈岿认为：“如果被征收人认为征收不是为了公共利益 或者征收补偿不合理，与政府的意见不一致，那么，政府有权单方面做出决定，被征收人也有权就这个决定向法院提起诉讼”。同样按我国特色，我们知道“被征收人也有权就这个决定向法院提起诉讼”这句话几乎是没有意义的，而“政府有权单方面做出决定”却是一定能落到实处的。<br />
<br />
2010.1.26.-2010.2.4.</div>

							
							
							
							
