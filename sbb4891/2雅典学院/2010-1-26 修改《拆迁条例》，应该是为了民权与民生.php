																											<div id="postmessage_466035" class="t_msgfont">十一、关于征收或者拆迁的立法取向问题<br />
<br />
旧的《城市房屋拆迁管理条例》最大的问题是立法取向的问题。旧的《条例》考虑的不是保护公民的人权和财产权，而是考虑怎样绕过这些宪法权利，更便利地拿走他们的房子和土地。<br />
<br />
修改旧《条例》的时候，最重要的是改变以往错误的立法取向。假如大的方向和思路不改，仅仅在条文上修修补补，只能会延长恶法的生命，或者产生一部新的恶法。<br />
<br />
十二、关于法律的长期执行<br />
<br />
中国的问题不仅在于缺少良法，更在于有法不依。实际上，即使是有缺陷的法律，假如从事司法和行政的官员能够尊重法的精神，心存公义，奉公守法，事情也是可以做得好一些的。<br />
<br />
中国人的问题不在于认识不到真理，而是在于没有长性，不能够持之以恒。特别是近代以来，中国人惯于把任务置于法律之上，为了一时的便利破坏法律。<br />
