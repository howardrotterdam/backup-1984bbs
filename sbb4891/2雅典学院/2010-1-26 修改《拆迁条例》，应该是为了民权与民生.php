首先，不可以随意采取强制措施。其次，合法的强制措施只能由执法人员来执行。<br />
<br />
利用和纵容非执法人员驱赶居民，是严重的违法犯罪行为。<br />
<br />
9. 在征收或者拆迁过程中，不可以中断司法程序<br />
<br />
司法独立，公民有向法院提起诉讼的权利，这本来是社会的常态。考虑到司法并不独立的现状，有必要指出，不可以在过程拆迁中对居民关闭法院的大门。<br />
<br />
法院不可以拒绝受理有关征收或者拆迁过程中的诉讼请求，不可以让征收或者拆迁活动成为法律的空白地带。一定要终止“法院为城市建设保驾护航”的谬误，进而终止法院为政府服务的违宪逻辑。<br />
<br />
10. 在征收或者拆迁过程中，需要媒体的充分报道<br />
<br />
舆论监督，是保障人权，确保廉政的有效手段。舆论监督的缺失，在拆迁问题上尤为严重。规模大一些的拆迁项目一旦启动，媒体通常采取回避的态度。人人都知道，报道拆迁是有危险的。最终的结果，就是黑幕重重。<br />
<br />
今后，必须确保社会舆论对征收或拆迁全过程的监督。对于征收或拆迁的立项、进展、资金的去向以及可能发生的暴力事件，媒体应该进行充分的报道。对于暴力拆迁事件，应该像对矿难一样重视。<br />
<br />
11. 必须严惩暴力拆迁的策划者、组织者、实施者<br />
<br />
对于暴力拆迁的策划者、组织者、实施者，不论是政府官员、企业管理者还是个人，都必须加以严惩。<br />
<br />
对于纵容及不积极制止暴力拆迁的官员，应该以渎职罪加以惩办。<br />
<br />
12. 文明执法，确保必要的强制措施<br />
<br />
对于以公共利益为目的征收或者拆迁项目，对于驻地居民以合法程序通过的商业征收或者拆迁项目，强制措施有时是必要的。<br />
<br />
我们要做的，是减少强制措施，避免强制措施导致暴力。需要特别注意的是，人的生命高于各种任务。<br />
<br />
后面还要指出，一定要减少大规模的征收或者拆迁项目。</div>

							
							
							
							
