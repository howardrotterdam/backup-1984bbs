																											<div id="postmessage_454861" class="t_msgfont">五、关于征收或者拆迁的公民权问题<br />
<br />
1. 发展不是硬道理<br />
<br />
“发展是硬道理”的提法，曾经有着积极的意义。问题在于，把这个有策略性和时效性的提法当成纲领，必然会导致片面和极端。既然发展是硬道理，公民权利就变成了软道理。<br />
<br />
最早的时候，有人强调等到经济发达了，公民权利就会提高。以后，又有人把发展当做理由去侵犯人权。最后，侵犯人权已经成为某些人暴富的捷径。就拆迁来说，最核心的问题在于权势者其实是在用掠夺的方法获利。<br />
<br />
因此，现在必须反对“发展是硬道理”的说法。<br />
<br />
2. 没有平等的谈判权，就没有公平合理的补偿<br />
<br />
关于拆迁的评估，过去是政府或拆迁方单方面定价，现在又试行市场定价。而在现实生活中，居民没有与政府平等的谈判权，就不会有公平合理的补偿。<br />
<br />
补偿合理不合理，不能够由单方面说了算，更不能由强势者说了算。当然，居民一方的要价也不可能全都是合理的，合理的价格就是由谈判产生的。<br />
<br />
谈判是艰难的，而没有谈判的社会则以一时的便利积累长远的隐患。<br />
<br />
3. 保护业主的地权<br />
<br />
目前，中国城市的土地是国有的。土地国有制的缺陷，已经是很明显了。在土地国有制没有改变之前，确保房屋业主的国有土地使用权，是一个保护私有财产的变通方案。<br />
<br />
在变通方案之下，业主的土地使用权是私有财产的一部分，受到法律保护，政府无权随意收回。土地国有是说土地属于国家，并不是属于政府，政府只是受国民委托管理这些土地。政府对土地的管理，要在法律允许的范围内进行，要受到国民和社会舆论的监督。<br />
<br />
新制定的法律或者法规，应该减少政府对国有土地的支配权。规模大一些的土地使用权变动应该经过听证，并且交由地方人大审议。<br />
<br />
征收或者拆迁的补偿，理应包括业主的国有土地使用权。<br />
<br />
业主的土地使用权进入谈判的议题，并且不可以被无偿或者低价收回，将会减少政府实行征收或者拆迁的冲动。<br />
<br />
4. 征收或者拆迁的补偿不仅仅包括房屋和土地使用权<br />
<br />
在征收或者拆迁的时候，有时仅仅补偿房屋和土地使用权是不够的。举例来说，在城市交通尚不够便利的时候，强行把居民迁到远郊区，使得有些人远离工作地点，造成极大的不便。比如，一个人在胡同里摆摊修自行车，搬迁之后就有可能会失去生计了。再比如，一家饭店搬迁以后，在经营上有可能大不如前，甚至一蹶不振。<br />
<br />
征收或者拆迁的补偿包括上述考虑，不但能够照顾到民生需要，而且能够减少社会动荡，减少征收或者拆迁的随意性。<br />
<br />
5. 居民有不搬迁的权利<br />
<br />
居民有不搬迁的权利。比如，我在平房住惯了，不想住平房。再比如，我从小住惯了。在商业征收或者拆迁中，居民也有权主张“给多少钱都不搬”。<br />
<br />
对于居民不搬迁的权利，有两个方面的问题。第一，这种权利不一定能够得到体现。第二，这种权利是征收或者拆迁必须考虑的。<br />
<br />
征收或者拆迁即使是合理的，过多地侵犯居民不搬迁的权利，哪怕是合法地侵犯，也是不好的，要加以避免。<br />
<br />
6. 大规模的搬迁和移民，有可能导致苛政或者暴政<br />
<br />
必须指出，频繁的、大规模的搬迁和移民，会扰乱正常的社会生活，威胁到社会的安定。特别是强制性的大规模搬迁和移民本身就是苛政或者暴政，至少会导致苛政或者暴政。<br />
<br />
中国的历史和现实都能够证明这一点。<br />
<br />
7. 人民有免于恐惧和掠夺的权利<br />
<br />
即使大规模的搬迁完全合乎法律程序，过多和过大的搬迁仍然会造成社会的恐慌和动荡。此时，公众和媒体有权发出声音，制止这种趋势。<br />
<br />
人民有免于恐惧和掠夺的权利。从现在起，再也不应该容忍暴徒骚扰、驱赶、伤害居民的事件了，再也不能够容忍拆迁中不等价的交换甚至掠夺了。</div>

							
							
							
							
