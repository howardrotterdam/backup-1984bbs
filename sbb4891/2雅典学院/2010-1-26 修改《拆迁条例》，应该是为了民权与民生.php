																											<div id="postmessage_452382" class="t_msgfont">三、把征收或者拆迁过程置于阳光之下<br />
<br />
1. 实现征收或者拆迁全过程的透明与监督<br />
<br />
征收或者拆迁关系到民权与民生，关系到巨额财产的交换与转移，关系到城市的未来。因此，为了市民的生存与安全，为了防止大规模的腐败，为了建设一个宜居的城市，必须将征收或者拆迁的全过程，包括立项、评估、谈判的进程、居民迁移的过程以及资金的去向，全部向社会公开，接受社会的监督。<br />
<br />
2. 对立项过程的规范与监督<br />
<br />
在以往的拆迁事例中，立项的随意性是一个很大的问题。通常是好大喜功，成片拆迁，随意扩大拆迁范围，增加被拆迁户的负担，破坏有历史价值的街区等等。总的来说，就是可拆可不拆的一定要拆，小拆和大拆之中一定要选大拆。<br />
<br />
因此，必须对征收或者拆迁的立项过程实行听证，并且听取专家以及社会各方面的意见。大一些的项目，还应该经过地方人大审议。<br />
<br />
一定要保障媒体对征收或者拆迁项目报道和批评的权利，允许社会舆论对立项的动机与合理性提出质疑。一般来说，过于夸张的方案，总是与以权谋私连在一起的。<br />
<br />
3. 严格限定公共利益的应用范围<br />
<br />
必须严格限定公共利益的应用范围，在法律和法规上加以严格界定，在实际应用中采取偏严格的限制。<br />
<br />
关于这一点，还需要更多的讨论。<br />
<br />
4. 对征收或者拆迁的补偿及过程实行监督<br />
<br />
在立项的听证阶段就应该论证征收或者拆迁的补偿标准，专家、公众和媒体应该能够对补偿标准的合理性做出分析和批评。<br />
<br />
如前所述，对于征收或拆迁项目的进展、资金的去向以及可能发生的暴力事件，媒体应该进行充分的报道。对于暴力拆迁事件，应该像对矿难一样重视。<br />
<br />
5. 必须实行回避制度<br />
<br />
不论在征收或者拆迁的立项还是以后的建设之中，都必须实行有利害关系的官员回避的制度。用民间的语言来说，就是要考查一个项目上马的时候，有关官员的妻子、儿女和好友要在其中扮演什么角色。加入存在着利害关系，那位官员就应该回避。<br />
<br />
6. 用于征收或者拆迁的各种资金（包括补偿资金）应该是可核查的<br />
<br />
必须把用于征收或者拆迁的各种资金，其中包括补偿资金置于有效的监管之下。给予搬迁居民的补偿金不但要合理，而且要从专项资金中直接划拨，不允许截留。<br />
<br />
资金的总额、去向应该透明，账目清楚，要接受审计，并且接受社会的监督。我们要争取的，还有公布资金使用的明细。<br />
<br />
7. 绝不能让公司承包搬迁和补偿<br />
<br />
让公司或者其他经济实体来执行搬迁任务，并且在补偿资金使用上实行承包，是把公权授予私人，公权私用，以公权谋取私利的恶例。这样做，虽然提高了行政效率，却给权钱交易打开了方便之门。由于在补偿资金使用上实行承包，就使得拆迁公司随意克扣搬迁户的补偿金，以及用恐吓和暴力手段驱赶居民。<br />
<br />
8. 一个难题：商业项目征收或者拆迁的居民表决及实施<br />
<br />
这个问题，需要深入的研究和讨论。</div>

							
							
							
							
