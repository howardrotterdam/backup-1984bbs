																											<div id="postmessage_451598" class="t_msgfont">一、背景：暴力拆迁与《城市房屋拆迁管理条例》<br />
<br />
1.必须正视和反省以往拆迁过程中的问题<br />
<br />
近年来，中国在经济上的成就掩盖了发展过程中的问题。中国的城市发展，就是以不公平甚至野蛮的拆迁为代价的。在这一过程中，公平交易的原则被忽视，公民的财产权遭到了侵犯。在很多案例中，公民还遭到了恐吓与胁迫，有些公民被伤害，有人甚至被杀害。更为严重的是，一旦拆迁项目启动，有关拆迁的民事纠纷很难被法院受理。面对暴力拆迁，执法部门常常不作为，实际上是纵容了违法犯罪行为。<br />
<br />
由于缺少社会的制约，拆迁的决策常常表现出高度的随意性。大拆大建，频繁的拆建已经成为普遍的现象。就在近几年，大批有历史价值的建筑和街区被毁掉了。由于拆迁过程以及拆迁资金的去向不透明，在很多拆迁案例中可以说是黑幕重重。<br />
<br />
由于经济发展方式中存在的问题，中国的经济过于依赖基本建设，最后走上了以拆旧建新来发展的歧路。现在，甚至出现了以拆新建新为代价的发展。<br />
<br />
必须指出的是，中国的经济已经走进了死路。从事制造业和服务业已近无利可图，大量的资金进入了房地产行业。在这种情况下，依仗行政手段，靠拆迁中的不等价交换获利，已经成为一些地方政府的财政来源，成为权势者获得暴利的途径。<br />
<br />
因此，在修改《条例》，回顾过去的时候，绝不可以用“以往的城市建设取得了巨大的成就，积累了成功的经验，同时也存在一些问题”作为搪塞。我们必须正视问题，认真地反省以往的过失。我们应该认识到，过去那种不正常的拆迁模式助长腐败和扰民，埋下了巨大的隐患，必须立即废止。<br />
<br />
2. 2001年《城市房屋拆迁管理条例》是一部恶法<br />
<br />
2001年开始施行的《城市房屋拆迁管理条例》是一部恶法。《条例》不符合宪法精神，首先考虑的不是保护公民权利，而是怎样更方便地从公民手中拿走住宅。关于这个问题，已经有很多论述。<br />
 <br />
师安宁在《废止中国二十一世纪初最大的“恶法”――《拆迁条例》》中，指出《条例》有“四硬一软”：<br />
 <br />
《城市房屋拆迁管理条例》针对被拆迁人设计了四项强制性制度：第一是强制许可制度。在尚未征求被拆迁人意见的情况下，行政机关即可用《条例》所赋予的拆迁许可权向拆迁人发放拆迁许可证，此时被拆迁人的被强制拆迁的命运几乎已被注定；第二项是强制签约制度。拆迁人获得拆迁许可后，《条例》规定拆迁双方负有签约义务。不管被拆迁人是否愿意拆掉自己的房子，其有义务与政府或开发商签订拆迁协议，意味着被拆迁人对自己的物权必须作出让与；第三项是强制裁决制度。由于补偿标准是拆迁协议的核心问题，标准过低几乎是所有拆迁项目的共同特征。被拆迁人往往在无法抗衡被拆的命运的情形下，退而寻求能获得较高的补偿，并籍拒绝签订拆迁协议以求自力救济，但《条例》规定，当达不成拆迁协议时，任何一方可申请拆迁行政管理部门裁决。现实中几乎均是开发商或是由政府组建的城市建设投资公司之类的组织请求强制裁决，裁决结果也几乎都是一律拆迁，补偿标准也难以发生实质性的变化。被拆迁人对裁决结果几乎都不服，但接着由第四项强制制度可保障将拆迁进程继续到底，因为《条例》规定拆迁人可申请行政强制或司法强制执行，通过司法或行政途径将被拆迁人强制迁出原居所地。除上述四项强制性的“硬”制度外，《条例》还规定了一项“软”制度，即补偿评估制度。受政府或开发商“雇佣”的评估机构根本无法将市场价格原则和重置价格原则充分地贯彻到拆迁评估事项中，被征收的物权人只能获得与实际市场价值相比严重“缩水”的补偿额。</div>

							
							
							
							
