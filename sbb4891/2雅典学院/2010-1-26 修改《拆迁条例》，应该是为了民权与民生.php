刘松萝<br />
<br />
2007年，物权法通过并且生效了。那时，公众希望延续了很多年的强制拆迁能够从此而终结。即使无法终结，也要有所减少。应该说，公众的愿望落空了。<br />
<br />
物权法生效以后，2001年开始实行的不符合宪法精神，不符合2004年宪法修正案的《城市房屋拆迁管理条例》（以下有时简称为《条例》）却没有得到修改，继续在发挥作用。<br />
<br />
物权法刚刚生效的时候，强制拆迁一度有所收敛。过了一些时间，强制拆迁又开始抬头。特别是美国金融危机爆发以后，为了刺激经济，保障经济发展的速度，强制拆迁的范围和力度甚至达到了空前的程度。在这中间，血腥的暴力事件不断出现，直至发生了唐福珍自焚事件。<br />
<br />
必须指出，造成种种弊端的原因，就是权力大于法律的专制理念以及任务大于法律的机会主义治国理念。<br />
<br />
现在，《城市房屋拆迁管理条例》终于要修订了。我认为，对《条例》的修订必须十分认真、郑重和慎重，必须征询社会各方面的意见，必须在全社会展开讨论。<br />
<br />
必须指出，2001年《城市房屋拆迁管理条例》是一部恶法。我们一定要警惕，假如对《条例》仅仅修修补补，那就不仅会形成另一部恶法，而且还会赋予恶法新的生命力。<br />
<br />
作为最低限度，应该明确地废除2001年《城市房屋拆迁管理条例》，制定新的条例。作为要争取的目标，应该是制定《不动产征收法》。<br />
<br />
为此，特提出修改《条例》应该注意的问题和应该遵循的原则。<br />
