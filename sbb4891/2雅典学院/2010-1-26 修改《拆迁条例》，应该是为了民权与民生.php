																											<div id="postmessage_453233" class="t_msgfont">四、关于征收或者拆迁的宪法问题<br />
<br />
1. 任务不能高于宪法和法律<br />
<br />
对于宪法的权威，没有人提出疑问。但是，在现实的操作当中，宪法却要让位于官员和任务，其地位还不如方针政策。尽管一再地反省过去有法不依的教训，只要一有新的任务，比如抵御金融危机、举办奥运、扩大内需等等，宪法和法律就会再一次让位于任务。<br />
<br />
针对征收或者拆迁问题，我们必须警惕今后一旦有新的任务出现的时候，以往的问题会再一次出现。<br />
<br />
我们必须认识到，任务不能高于宪法和法律。<br />
<br />
2. 政府行政的非营利性原则<br />
<br />
政府的主要责任，按照旧时代的观念，是守土之责。按照现代观念，是保持社会的安定和秩序，维护社会公平，让国民安居乐业。<br />
<br />
政府的行政，不能以营利为目的。宏盛最近在文章里引用了《大学》中的话：“国不以利为利，以义为利也。”他认为，“这句话提出了一个至今仍非常适用的宪政原则，即政府只能以适当的税率收税的形式、而不是经营一般性商业的形式获得提供公共物品的资源。”<br />
<br />
3. 政府在经济活动中的被动性、中立性和非营利性原则<br />
<br />
经济的发展，并不是政府的直接目标。政府有时需要通过财政和税收手段来刺激经济或者调节经济，但总的说来，政府在经济活动中应该遵循被动性、中立性和非营利性原则。<br />
<br />
就是说，政府可以干预经济，但不可以过度，也不可以在干预经济的过程中获利。<br />
<br />
4. 政府不应该在征收或者拆迁中获利，不应该在城市建设中获利<br />
<br />
做到，或者部分做到这一点，就可以减少大规模的拆迁，减少扰民了。<br />
<br />
5. 改革，必须包括限制政府的权力<br />
<br />
改革初期，我们认识到权力过于集中的管理体制阻碍了经济的发展。那时，曾经放松了对经济的管制。随着经济的发展，经济自由有了长足的进步。<br />
<br />
但是，暂时放松对经济的管制并不意味着权力作用的下降。上个世纪末，为了抵御东南亚经济危机的影响，政府再一次直接地介入到经济发展之中。<br />
<br />
既然改革初期的放权是为了经济发展，那么同样为了经济发展自然就可以集权了。集权的结果，是经济发展方式出现了问题，腐败日甚一日。<br />
<br />
现在，已经到了限制政府权力的时候了。<br />
<br />
6. 必须否定“经营城市”的理念<br />
<br />
“经营城市”，曾经是一个很时兴的理念。城市的大拆大建，政府对市民生活的随意干扰，就是在这种理念指导下发生的。<br />
<br />
现在，已经到了摈弃“经营城市”的理念的时候了。<br />
<br />
7. 政府不可以公司化，不可以与民争利<br />
<br />
政府热衷于拆迁，热衷于房地产，势必成为类似于公司的营利机构。不仅如此，政府之利是以垄断地位从市民手中攫取的。<br />
<br />
决绝好征用或者拆迁问题，一是要限制政府的权力，而是要改变政府与民争利的现实。</div>

							
							
							
							
