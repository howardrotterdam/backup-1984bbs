　　在唐骏讲演前，我在休息室陪着他，看他还拿了张纸，用笔在上面飞快的写字，好像是讲演的提纲。一些具体事务，都是他的两个女助手来负责接洽。估计像他这样的人，每分钟干什么，都是计算好的。他上午的讲演一结束，马上又赶回酒店，然后去中山大学讲演，讲好之后，马上赶往机场，简直是一刻都没停。<br />
