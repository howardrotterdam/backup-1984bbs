　　是不是骗子，现在也许大家都在想这个问题，但是我可以说至少他是骗了。首先，学历、相关的履历造假这是骗。第二，身世也是骗。特别是学历和相关履历造假，骗取公众信任，这一点白纸黑字写在那里，他自己也承认。 <br />
<br />
　　主持人： <br />
<br />
　　王教授，您是法律的学者，如果按照您刚才的分析，按照这几条，够把这个人怎么判了？ <br />
<br />
　　王锡锌： <br />
<br />
　　我觉得其实在过往我们已经看到类似的这种个案，包括刘太医还有林光常，他们其实尽管名目不一样，但是真正的目的都是一样的，就是要用某种隐瞒的方式获取公众的信任，最后牟取钱财。如果从法律角度来说的话，这就是比较典型的商业上的欺诈。 <br />
<br />
　　主持人： <br />
<br />
　　他这个造假，比如说他没有这样的教育背景，他没有这样的职业证书，他没有这样的资质，他却说有，他触犯的是什么？ <br />
<br />
　　王锡锌： <br />
<br />
　　他是一种捏造虚假的信息。这种捏造虚假信息我们看他的目的是非常清楚的，比如说，所谓的这种“神医”，他没有去捏造其他方面的履历，而都是往“医”这方面来靠，包括医科大学的临床学位，还有各种方面跟医有关的履历，他其实就是要获取公众的信任。 <br />
<br />
　　主持人： <br />
<br />
　　但是他自己说了我不是医，我开的不是药，我开的是食疗，你爱信不信？ <br />
<br />
　　王锡锌： <br />
<br />
　　虽然这么说，这么说我可以看到他实际上是吸取了一些教训。因为我们原来看到刘太医，刘太医这个人可以说是比较低级的行骗，为什么呢？他犯了一个主要的错误，他就是来制造一些药物然后出卖，可是这时候相关的部门，比如药监部门最后来查了。所以在这里他想打这么一个擦边球，那就是我是搞食疗的，我开的不是药，是食物。但是不要忘了，他在捏造这些虚假学历、背景过程中，他可是都往医上面靠了。 <br />
<br />
　　主持人： <br />
<br />
　　你看，张悟本的这件事到目前来说，他手艺到底是真伪我们还真无法下断言，但是他身份的真伪我们却一望即知。现在的问题就出来了，所谓这样一个“神医”的身份，到底是怎么锻造出来的呢？ <br />
<br />
　　(播放短片) <br />
<br />
　　解说： <br />
<br />
　　如果不是张悟本的迅速走红，人们也很难注意到这个并不起眼的门面。这个镶嵌在奥林匹克中心西南口的小店铺，就是大名鼎鼎的悟本堂。 <br />
<br />
　　根据媒体的披露，这里是张悟本平时坐堂的地点之一，普通咨询号300元，排队的人已经排到2012年年初，加急见张悟本挂号费则为1800元到2000元。 <br />
<br />
　　今天当我们的记者赶到这里时，里面并没有前来求医问诊的公众。不过，今天下午门店外面多了两辆北京工商部门和卫生部门的车辆，据我们的记者了解到，两个部门和悟本堂负责人进行了闭门面谈，但始终没有人愿意接受媒体的采访。 <br />
<br />
　　记者： <br />
<br />
　　他这个店在这儿开多久了？ <br />
<br />
　　市民： <br />
<br />
　　过完春节以后吧。<br />
 <br />
<br />
 <br />
记者： <br />
　　过完春节以后才开，就是这几个月的时间，这么短？ <br />
<br />
　　市民： <br />
<br />
　　对。现在好像是不预约了。<br />
 <br />
<br />
 <br />
<br />
<br />
记者： <br />
　　您见过那些人吗？ <br />
　　市民： <br />
<br />
没有，没见过。 <br />
<br />
　　同期： <br />
<br />
　　掌声再热烈一点。 <br />
<br />
　　老师你太火了。 <br />
<br />
　　我不是最贵的，我是世界上最便宜的医生。 <br />
<br />
　　解说： <br />
<br />
　　张悟本这个名字为公众所熟知，是从这档电视节目开始。自从2010年年初三度现身在栏目，让张悟本迅速走红，不少人慕名找到了位于北京的悟本堂。 <br />
<br />
　　市民： <br />
<br />
　　有的是从外地来的，他就是打电话他那儿也没人也，预约不上人就走了。 <br />
<br />
　　解说： <br />
<br />
　　互联网上网友为张悟本搜索到12个头衔，大部分头衔媒体均通过调查采访予以了否认。12个头衔中，我们目前唯一能够肯定的只有“北京悟本堂中医研究院的首席专家”，然而这家机构的性质人们却很难定义，由张悟本本人亲自撰写的机构简介中他这样写到：同仁堂、全聚德等一大批中华老字号，正在努力通过品牌化，解决发展中知识产权、融资等机制问题，这也正是悟本堂希望努力探索的发展方向。 <br />
<br />
　　张悟本： <br />
<br />
　　食既能充饥，也能疗疾，你能做出得病的饭来，那你为什么做不出治病的饭呢，对吧？ <br />
<br />
　　祝肇刚 著名中医： <br />
<br />
　　中医是个学问，它是踏踏实实几十年积累积累的，它不是一个遗传的东西，它是个传承的东西，必须要你耐得住性子，踏踏实实去学去。像我跟学生讲，你们学医的别做名医，出名的名，要做一个明明白白的医生，明医。 <br />
<br />
　　解说： <br />
<br />
　　祝肇刚，北京金方国医院副院长，著名中医祝谌予教授之子，曾任北京第三针织厂厂医。有媒体报道，张悟本曾是该厂的一名普通工人，并经常找祝肇刚切磋医术。 <br />
<br />
　　祝肇刚： <br />
<br />
　　这人我一点印象都没有了，因为当时病人多极了，找我看病的人也特别多，我真不记得哪个人是哪个人了。 <br />
<br />
　　记者： <br />
<br />
　　现在许多的名医或者是养生专家，他总是背着一个光环或者是头衔，这是为什么？ <br />
<br />
　　祝肇刚： <br />
<br />
　　举个例子，就跟印名片似的，你就印一个名片，你是个大总统，人家给钱就给印呗。光环这个东西谈不到，再说谁愿意信这个，谁要愿意信谁就信，你说是不是？ <br />
<br />
　　主持人： <br />
<br />
　　我们刚才看到的是张悟本，我们不妨再认识两位。左边这个年轻一点的林光常，叫“红薯王子”，右边这位年老一点儿的叫“刘太医”，也是曾经掀起过一阵热潮。其实你对比一下他们三个人有很多相似的地方，都是先出一本书，这位写的是《无毒一身轻》，那位写的是《刘太医养生》，然后就是被媒体关注到了，然后就是操作，最后也是由于媒体关注让他们的事情曝光了。你怎么看他们这种类似像公式一样的所谓“成名路径”？ <br />
<br />
　　王锡锌： <br />
<br />
　　其实我觉得真的是殊途同归。这里面的“同”最重要的，之所以能够在结果上“同”，其实不论他们以什么样的名目出现，不论是刘太医要卖所谓的通玄散，还是林光常所谓“地瓜王子”要卖红薯。<br />
 <br />
<br />
 <br />
<br />
<br />
<br />
质上我看三个方面是非常相同的：第一，他们都要包装自己，包装自己其实都要给自己戴上各种各样的帽子，制造一种光环。而且这种包装是通过一些所谓的大众媒体，通过媒体来包装，这样老百姓容易相信他们；第二，他有一些欺瞒。有许多事情可能他们自己明白，刚才我们这位著名的中医谈过要做明明白白的医生。可能有些人从常识来说他就明白，但是为了赚钱他可能要让老百姓不明白。第三，说得简单一点就是谋财，因为做这些最后的目的，就是要把别人的钱掏过来。这三点是共同的，如果这么做的话，我觉得结果共同――受到法律的制裁，这应该是公理所在 <br />
<br />
 <br />
<br />
<br />
<br />
主持人： <br />
　　你看刚才我们说到了张悟本、刘太医这些人，他们真的就像孙悟空从石头缝里面蹦出来一样，一下就出现在我们的面前。作为观众也好，作为读者也好，我们到底怎么去分辨他们是真的还是假的。就这个问题我们来连线北京协和医学院公共卫生学院的院长黄建始先生，黄建始先生也是《中华健康管理学杂志》的副总编。 <br />
　　黄先生您好。刚才我们看到的几位所谓的“神医”，现在我们可以从电视上看到他们的讲座，从书店里面可以买到他们的书籍。我们觉得，作为普通老百姓特别难分辨，他们到底是真的、假的，分辨这件事应该谁来做？ <br />
<br />
　　黄建始 北京协和医学院公共学院院长： <br />
<br />
　　应该是每个人自己来做，因为像这种问题，为什么老是上当受骗，这应该值得我们整个社会来考虑。为什么这么说呢？有句话说，你第一次上当有可能发生，第二次上当我们就要考虑一下我们自己的问题了。 <br />
<br />
　　主持人： <br />
<br />
　　但是黄教授，我不同意您的观点。您看，如果我作为观众的话，电视台播了，然后国家的出版社出版了，我作为一个受众，分辨的这个义务应当由我来完成吗？ <br />
<br />
　　黄建始： <br />
<br />
　　健康应该每个人都是自己的责任人，你对于你自己的健康当然应该是你自己完成，这是个理念问题。为什么会出现这样的问题呢？我想两个主要的原因：第一，我国国民普遍来讲，健康素养有很大的发展空间。卫生部最近公布国民健康素养调查发现，中国人健康素养及格的只有6.48%，也就是说，100个人里面有96个是健康素养不及格的。 <br />
<br />
　　主持人： <br />
<br />
　　黄教授，抱歉，我再打断您一下。的确，我们的健康素养是不高，但是我们的健康素养通过什么样的方式得到呢？最普通的人往往就是通过电视和书这种方式。你看，他传递了一个错误的信息，我们怎么去分辨呢？ <br />
<br />
　　黄建始： <br />
<br />
　　所以这里面就是，整个社会，包括政府、社会都有提高这方面的认识。有个很重要的问题就是，为什么原来不会出现这样的现象，最近这种现象屡次出现？因为现在中国人对健康关心了，而现在对健康关心观念没转变。中国人是饿了千千万万年，一直到最近10年来才基本上吃饱了，饿肚子的时候生病是可以有药治，可以治好的，吃饱了饭以后生的病，大多数都是要靠改变环境和改变自我行为来改变的，而这个东西就需要提高健康素养。什么叫“健康素养”？就是利用健康信息来管理自己健康的能力。这个问题每个人都要知道，比如说怎么吃？我们国家1997年就公布了中国居民的膳食宝塔，2007年、2008年卫生部又再次重新公布了一遍，这些信息都摆在那里，大家不去看。就好比说，你连1+1都不懂，所以我给你讲1+1=10，你相信我，我讲1+1=8你相信我。但那明明摆在那，假如你懂1+1=2，我再怎么讲你都知道我是骗子，是不是？ <br />
<br />
　　主持人： <br />
<br />
　　好的，谢谢黄教授，您呈现的是您的观点，接下来我们再听听，王教授，您同意黄教授的观点吗？ <br />
<br />
　　王锡锌： <br />
<br />
　　其实黄教授作为北京协和医学院公共学院的院长，我更多理解他是一个公共卫生方面的专家，因为他强调了公众不仅仅要有保健的意识，而且要具备这种健康的常识，应该说这一点有它一定的道理。但是，如果我们把这一点当做是来监管某些这种“伪医”，“伪食疗专家”。当做这个理由恐怕，一方面来说它可能不是长期的，再一个，它可能使得许多这种假冒的所谓“神医”、假药等等大行其道。为什么呢？因为我们看到，民众关注健康，这完全是情有可原的，这是第一方面。第二个我们要注意，在各种各样食疗、养生的背后，其实许多老百姓来找所谓的“神医”，他是有病的，而且有的往往是绝症，这些神医他都是号称能治癌症，你想癌症现在是不治之症。 <br />
<br />
　　看病难、看病贵，现在我们这些所谓的“神医”都切中了这种民众的心理，那就是看病很难，我告诉你不难，你自己就是医生，看病很贵，我告诉你菜场就是药厂，你去吧。所以这时候他抓住了民众的心理，民众要来防很难，因为这里有一个心理上的问题。 <br />
<br />
　　主持人： <br />
<br />
　　王教授，其实刚才黄教授说了一个很重要的观点，我们姑且不说它的对错。他说95%左右的中国人对于健康最基本的常识是没有获取的，但问题是有一个谁先谁后的问题，像这种基础的健康常识，我们往往通过电视、广播或者书籍这样的方式获得。但是如果这个时候这个环节出了问题，我们有一个先入为主，如果错误的先进来了，我们又怎么知道哪个是正确的？ <br />
<br />
　　王锡锌： <br />
<br />
　　没错，所以这里面提到的就是，民众要有这种意识，但民众的公共卫生、公共健康意识…… <br />
<br />
　　主持人： <br />
<br />
　　什么渠道？ <br />
<br />
　　王锡锌： <br />
<br />
　　又要取决于一些平台、一些渠道。 <br />
<br />
　　主持人： <br />
<br />
　　没错。 <br />
<br />
　　王锡锌： <br />
<br />
　　我们就以这个张悟本的个案来说，他为什么能红起来？是借助媒体。而且你看许多事例都表明，他在湖南卫视做《百科全书》的节目以后一下红了。这个平台很重要，平台我们知道，实际上这样的平台提供公众就会来关注，以为你这样的平台上面所讲的就是权威的。 <br />
<br />
　　主持人： <br />
<br />
　　我信任你的平台，所以我才信任这个人。 <br />
<br />
　　王锡锌： <br />
<br />
　　没错，也正因为这样我们强调，工商部门也好，卫生部门也好，对医疗广告和变相的医疗广告必须要监管，这个监管是政府的责任，也是这些媒体、这些平台要承担的一种社会责任。 <br />
<br />
　　主持人： <br />
<br />
　　没错，就是说当你作为一个公共发布的平台，你本身没有把关的时候，你怎么能期待着普通老百姓来作为一个把关者？ <br />
<br />
　　王锡锌： <br />
<br />
　　对。知识的获得需要渠道，如果渠道本身就出了问题，把关不严，这首先就有问题。</div>

							
							
							
							
