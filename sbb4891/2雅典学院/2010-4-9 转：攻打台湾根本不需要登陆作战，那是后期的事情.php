<br />
<br />
　　第三点：美军与日军在太平洋海岛上战争之所以惨烈，是因为美军的海军陆战队与日本的留岛部队实力相差不多，当然在海军和空军日本的实力就弱一点，主要原因是日本在中途岛海战与珊瑚海战主力航母消耗殆尽，日本在海岛争夺战失败的最主要原因是战术僵化造成的！而我军的实力跟台军不可同日而语！双方的差距太大，尤其台湾，甚至包括现在的日本没有战略纵深，美军为什么要把海军主力退守到关岛一带，也说明我军的整个战力尤其是海空军的战力的提升，对美军产生了很大的威慑作用，不然，美军的航母会在台湾海峡肆无忌惮！<br />
<br />
<br />
