													<h2>台海统一是至高无价的吗?</h2>
						
						
																											<div id="postmessage_555666" class="t_msgfont">台海统一是至高无价的吗?<br />
<br />
<br />
思芦 <br />
记得电影《南征北战》有一句台词：蒋介石的指挥棒从来都是受毛主席调动的。半个世纪过去，乾坤倒转。现在是中共的指挥棒受李登辉，陈水扁调动了。你看每当李陈二人选情不利时，就故意在独统问题上制造事端。而中共每每上钩，或威吓，或军演，或发飞弹。台湾选民偏偏吃软不吃硬。你越是威吓，他越要选台独分子。李陈二人上台，中共助选功不可没。弄得连战都害怕了，一再警告中共别给陈水扁帮忙。而陈水扁为了连任，故计重施，祭出独统公投法器。果然击中中共要害。两岸形势顿时紧张，车辚辚，马萧萧，大军滚滚奔闽南，为阿扁助选而来。两岸气温一升，网络就要打喷嚏。论战的有之，骂娘的也有之。大陆台湾统一是好事。都是同文同种，和则两利，离则俱伤。但是统一是不是无价？统一是不是一个民族的最高目的？为了统一，我们是不是应该不惜一切代价？<br />
<br />
一提起台湾，愤青们就说台海对中国的重要战略和经济地位。可是按照叶九条，中共对台湾的统一只是政治和名份上的。台湾除外交外，完全自治，可以保留自己的军队，解放军也不会进占台湾；台湾的资源即不能为大陆所用，大陆倒可能为台湾财政输血。除政治外，和平统一后，台湾对大陆的战略和经济地位与现状相比并无区别。如果武力统一，台湾经济完全打乱。台海形势紧张，中美军事直接对恃。台湾成为火药库。台湾的战略经济地位从何谈起？<br />
<br />
其实在领土主权考虑中，中共最感兴趣的是政治上的：消灭政治上的对手，维护自己的统治。先不说中印之战的退兵；也不说珠峰南麓的割让于尼泊尔；再不说江心坡、南坎割让给缅甸；还不说为了不让北韩“感情上过不去”，中共慷慨赠予长白山主峰的东半。就单说当年蒙古的独立。蒙古也是由公投决定独立的。而且公投是在苏联士兵的刺刀下举行的，其公平性很值得怀疑。但毛泽东当时即表示同意。中共建国后16天立即予以承认。当时能承认蒙古的民族自决权，现在又何以出尔反尔来反对台湾的公投？蒙古的战略地位不逊于台湾：俄国是中国的最大威胁，北部边疆无海洋屏障，外蒙可为北京提供大片战略缓冲之地。为什么轻易舍弃？而现在台湾却成为战略经济要地。要为其拼个鱼死网破。为什么要厚此薄彼？实话实说：当年为了取得苏联的支持，消灭国民党，不惜以领土为交换。现在为了消灭政治对手而统一台湾。战略和经济的考虑都是次要的，意识形态和政权利益才是中共的第一考虑因素。<br />
<br />
为了统一台湾，中共媒体大力宣扬施琅，甚至把施琅地位提高到郑成功之上。施琅是郑成功的部将，恃才倨傲，屡忤成功。得罪了郑氏，家人为其所杀。施琅投奔清廷。为报家仇，引清兵攻占台湾。小学课文里就知道郑成功是从荷兰人手里收复台湾的民族英雄。施琅背主叛明投清，替清朝从反清复明的郑家手里夺回台湾。按从前说法，是和吴三桂一样，是汉奸。现在又变成了高于郑成功的民族英雄。难道因为他打的是内战，郑成功打的是外战？<br />
<br />
纵观历史，国家争战，多为扩疆掠土。如果以统一作为评史的标准，历史教科书都要重写。 以此推论，不仅秦始皇可以因统一六国而被老谋子捧为英雄。其残暴和焚书坑儒都可被忘掉。而且春秋战国时各国诸侯也都可以因为是为统一而战而摘掉“春秋无义战”的帽子。金兀X和秦桧也应该被称为统一的英雄，岳飞反倒是抗拒统一逆历史潮流而动了。<br />
<br />
评历史以统一划线的人，应该去问问当时战乱中的老百姓，“宁作太平犬，不做乱离人” 是什么滋味？<br />
<br />
统一是好事。但如果统一非要搞得兵戎相见，中国人自相残杀，两岸人民生灵涂炭，就是不利于中华民族之坏事。凌驾在统一之上的，应该是人民的生活幸福安定，民族的长远利益。一个民族不应该统一至上，为统一不惜一切。得到一个焦土下的台湾。对老百姓有什么好处？此所谓“民为重，社稷次之” 。<br />
<br />
如果台湾一心要独立，怎么办？凉拌。天要下雨，娘要嫁人。没有法子的事情。三国演义开宗明义：分久必合，合久必分。天下大事，无非趋利避害。我们把自己的事情办好了，大陆经济发展了，祖国强大了，老百姓生活超过台湾。也许将来台湾就要屁颠屁颠地上赶着要求重新加入中华大同盟。东德西德不是又统一了吗？总比打得两败俱伤，埋下仇恨种子的勉强统一要好吧。只可惜的是，中共的领导人就少了一个在青史上留下象秦始皇那样的统一名声的机会。扬拳振臂的愤青们，也少了建功立业的机会，如果他们呐喊的打仗真是要自己上战场而不是让别人去的话。<br />
<br />
如果台湾非要独立。建议台湾把当年从中国带到台湾的故宫瑰宝，历史典籍归还中国。道理很简单，你不承认是中国人，你就不要占有中国的文化和历史。这些中华民族文化的遗产，才是真正无价的。<br />
<br />
<br />
<br />
独立诚可贵,<br />
统一价更高。<br />
若为人民故，<br />
二者皆可抛。</div>

							
							
							
							
