<br />
<br />
　　有人大放厥词，说美国在二战太平洋战争时期，动用了多少航母，战舰死了多少海军陆战队士兵才可以获得胜利，说，美军如此的实力一个硫磺岛战役，美军基本上是惨胜。。。。。等等，说什么，中国人民解放军现在根本就没有准备好，假如台湾独立，我们也没有办法，因为我们没有航母舰队，没有大批的登陆舰船。。。。我们没有准备好，将会失败告终。。。这类的蛊惑人心的话！<br />
<br />
<br />
　　你们其实是军盲，根本不懂现代战争的玄妙，中国如果按照二战美军的打法，那是愚蠢的，是没有效率的浪费，我军不会这样做的，其实台湾胆敢独立，我军仅仅两周时间就可以收复台湾，让宝岛重新回到祖国的怀抱！<br />
<br />
<br />
　　需要指出的是，战争的状况跟二战美军进攻硫磺岛的状况根本不一样<br />
<br />
<br />
　　第一，美军是远离本土的战争，而必然需要强大的航空母舰舰队。登陆舰对和护航舰队，以及更为强大的后勤保障支援舰队，这个是有区别的，而我军相距台湾不过200海里的距离（最近距离约123.44公里），根本不需要航母舰队，假如需要，也是在台湾东部或者在第一岛链外围做掐断敌国对台湾的支援！这一点我们必须看得很清楚，我军在东南沿海拥有数十个军用、民用机场和完善的保障，防护设施，另外还包括了贯穿东南沿海的高速公路体系以及战略纵深，我军的前线战机甚至要起飞后越过台湾进行折返作战这一点是根本要区别美军在太平洋作战的情形！<br />
<br />
<br />
