<br />
<br />
　　这样我军其实更多的需要飞豹战斗轰炸机、歼11战斗机歼10战斗机。。。。更多的需要反辐射导弹和反跑道集束炸弹或者智能精确打击炸弹、空对地导弹，而第一轮攻击，则更需要战术导弹或者巡航导弹对敌战略目标进行饱和式的攻击，1500－3000枚战术导弹与巡航导弹在30分钟内即刻瘫痪台湾岛；<br />
<br />
<br />
　　第二，在48小时之后，我军的空降15军应该在台湾主要城市进行空降，并迅速占领台湾如台湾、高雄、台东。。。等城市的民用国际机场，并在以此为基地建立我军登陆的桥头堡，一方面可以使大批的军用装备通过空运快速运抵第一线，而另一方面对台湾完成分割包围<br />
