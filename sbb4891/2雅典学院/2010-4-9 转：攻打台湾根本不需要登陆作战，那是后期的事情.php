													<h2>我们解放（收复）台湾什么？</h2>
						
						
																											<div id="postmessage_555646" class="t_msgfont">争鸣：我们统一台湾什么？[转贴]<br />
<br />
<br />
说到台湾统一，有几个思考一定要先提出来： <br />
1．我们是想统一岛上的那些土地吗？ <br />
2．我们是想统一岛上的那些人口吗？ <br />
3．我们是想统一岛上的那些财富吗？ <br />
4．‘和平’统一为什么会有这么难？ <br />
5．‘武力’统一的代价究竟有多大？ <br />
希望国家不分裂；世界上所有的民族都是同样，这根本不用讨论。我们许多‘爱国者’的认识，只是停留在‘统一’这个简单层面上，翻来覆去的‘吵’、‘骂’。一听说对方‘不愿意统一’，‘搞_’，就喊‘打’。这个‘打’字就这么简单？它的后果是什么？有多少人想过？ <br />
就冲我们大陆这么多充满仇恨，不问青红皂白就言‘开打’的人，台湾同胞谁敢寄人于这样环境的篱下？ <br />
我们‘义愤’的原因是台湾有人搞‘独立’。但那边究竟发生了什么？‘骂民’们有几个清楚的？ <br />
例如；陈水扁想把台湾卖给日本或美国，他们敢要？ <br />
再比如；陈水扁真的能搞成立陶宛、爱沙尼亚那样的独立吗？台湾有那样的基础吗？ <br />
退一步说；台湾宣布‘独立’，就如同‘访谈学者’说的；从中国版图‘分割出去’了？ <br />
事实上现在无论是大陆版图，还是台湾版图，本身就是‘分割’状态的。无论两岸的哪一边，你把地图印成一个颜色，就不是‘分割’了？ <br />
别说台湾还远没有到真正‘独立’，更别说联合国承认其合法性的可能性几乎没有。就说当初东、西德国，不但‘分割’了，还都是联合国及国际社会承认的二个独立国家。比台湾目前这个‘情绪_’，‘精神_’严重多了。可当时机成熟了；一架没吵，一枪没放，东、西德国一夜之间就统一了。 <br />
本文题目叫‘我们统一台湾什么？’，当然应有答案。我看答案就是统一‘人心’。 <br />
否则等了怎么多年，仅收回点土地和与我们二心的人口？如果是武力收回；就更加不值。 <br />
我们喊打的原因是因为‘和平统一’困难。那么什么是‘和平统一’呢？困难在什么地方？ <br />
在说‘和平统一’之前；先提出一个‘完全统一’的概念。这是因为不少人问；‘那边’的人为什么不愿意和我们一样？ <br />
让台湾成为一个省，由中央派大批的干部接管，给陈水扁，吕秀莲一个‘副省长’，军队整编归属解放军。这种愿望，用和平方法的可能性是零。这只能是武力拿下后出现。但那时的陈水扁，吕秀莲就不是‘副省长’了，而是‘号子’里面的‘副号长’了。 <br />
如有人硬要说为什么‘完全统一’没有可能？你们不用问台湾其他人，可以先去问整天唱高调的李敖和阮次山，问他们愿意被这样‘统一’否？台湾和美国被他们骂的一无是处，为什么不入我们大陆籍？当阮次山在清华大学演讲时，有学生质问他为什么入美国国籍，他居然说是：“美国政府三次主动送他，在三次拒绝后，为了旅游方便才收下的”。你们谁信这种哄三岁孩子的话？有个清华学生当天在BBS上写帖子：“我不知道阮次山曾经给美国作过什么惊天动地的贡献，或是救过美国总统一命？究竟是美国把护照送给他三次，还是他申请了三次美国护照？ <br />
对一个已经有‘伪民主’选举权和话语权的台湾百姓，让他们走回头路，按李登辉→蒋经国→蒋介石的时代逆行，恐怕是不大情愿的。 <br />
因此看来；‘和平统一’剩下的唯一可能，就是‘一国两制’。那么我们来看这个‘一国两制’可能性有多大。 <br />
在大多数人看来，香港、澳门已经有样板在前，台湾不也顺理成章吗？但恐怕不是这样。这里面有三个原因： <br />
一是香港、澳门在百年的殖民生活中，虽然经济模式和生活方式接近西方国家，但却没有形成近代民主制度足够的政治基础，尤其是人文基础。人们说那里是‘文化沙漠’；从浅层的社会现象看，是文化品位较低。但深层的是人们的公民意识，社会责任，政治使命这些观念较淡漠。与台湾比有些差距。这里说差距是指整体而言，不是说港、澳人都这样。是整体政治观念平均值不太高。 <br />
二是港、澳没有出现西方那样完整的政治体系和政府体制。甚至如台湾的现行政体，港、澳也没有达到。因此当初港、澳回归时基本不存在强行改制的问题。 <br />
三是港、澳当年属于时效有限的‘契约’型殖民地。契约到期后，回归是个顺理成章的事情。在回归前曾过闹些风波，仅仅是殖民者的心态原因，以及港、澳人的归属感和认同感问题。 <br />
而台湾在1945年已经由原中国政府从日本人手中收回，不存在从殖民者手中二次收回的问题。加之台湾民众的‘话语权’较多（我们叫‘假民主’也好），即使台湾当局愿意接受港、澳模式，也得过民意关。 <br />
因此；‘一国两制’在台湾实现的难度比港、澳要大得多。因为港、澳回归前可以说就没有‘制’，或者说仅有‘附庸制’。 <br />
台湾类型的‘一国两制’在世界上从没有先例。要创这个奇迹，两岸政治家包括民众都需要有极大的政治勇气和政治智慧。 <br />
政治勇气这个词，不是很容易理解的。不是许多人想象的；气势汹汹，咄咄逼人，无所畏惧。它往往是相反的意思，在一定场合有可能要示弱。 <br />
以色列前总理拉宾曾是以军总参谋长，打了一辈子仗。在他以前的个人思维里，从来不知道‘害怕’和‘退让’这二个词是什么意思。 <br />
当他做出巨大让步（相对自己的国家而言）与对方签定和平协议时，你们无法想象他顶住了来自国内民众，各团体，政府内部的多大阻力和压力。但这个连美国总统都称为“政治老师”的拉宾，在获得诺贝尔和平奖后，却死在本国‘愤青’暗杀的枪口下。用自己的生命换来了一个政治勇气的故事。 <br />
反之，什么是缺乏政治勇气和政治智慧的表现呢？ <br />
在蒋经国去世后，继位者李登辉与他的‘老领导’，‘老师’相比，就有明显的政治幼稚病。在和平进程出现僵局时；不但缺乏冷静，还干一些出格，甚至令人无法理解和容忍的事情。例如大谈与日本的渊源等。打架打不过时；有你这样拉‘帮架’的吗？拉谁也不能去拉日本啊。这不成了第二汪精卫吗？ <br />
另外；我们的政府有没有犯急躁呢？ <br />
小平先生在生前曾告诉我们例如钓yudao问题：“在解决不了时，中日双方先放一放，等更具有政治智慧的办法，或者更具有政治智慧的人出现再解决”吗？而我们的政治升温，舆论大战，封锁空间，军事恫吓，符合中央要全党坚持的‘邓理论’吗？ <br />
对台湾问题焦虑，缺少回旋思路，就一条道往前走。我们的‘外交基石’到了刻板程度；与所有国家交往的第一基础就是不许他们‘搭理’台湾。然后其它的事情都好商量。你有‘经济困难’说话。逼的台湾与我们在一些小国家身上拼‘金钱外交’。两边的钱不都是中华民族的吗？ <br />
我们的解释是防止台湾‘国际地位合法化’。等它的‘空间’狭小得薹ㄉ?媸保?匀焕赐督怠?br&gt; <br />
问题是这样的‘副作用’会有多大？而且，即使达到目的，是否也是本末倒置？ <br />
在台湾大选投票的前一天，我们国家发言人宣布，选陈水扁当总统后果极其严重，大陆不会坐视不管。这等于是说陈水扁当选，就是意味着开战。但；多数台湾人似乎吃了豹子胆，第二天偏偏硬投了陈水扁的票。在网上鼓噪的人，有几个能解释台湾民众的心情？ <br />
我们换个说法；在《我们为什么恨美国？》网上转贴后，有网友反驳说：“美国视我们为敌，我们为什么不能同样视他为敌”？也有人反驳说：“对抗美国是为了生存空间和民族利益”。我想；这二个网友也一定是主张对台湾强硬的人（百分之百）。那；你们就掉进自己设的逻辑陷阱里了。 <br />
别人视我们为敌，我们就视他为敌。如果这个‘真理’是‘放之四海而皆准’，那台湾人呢？他们怎么能视我们为友？ <br />
第二个网友陷的更深；既然“对抗是为了生存空间和民族利益”，那台湾人为了生存，就只能对抗了。 <br />
只能对抗，又没有回旋的理念，那就剩四个字：你死我活。这就是‘塔利班’的人生信念。 <br />
我们思维里有种‘大’的情结，‘大’的责任理念。大；就应该收回小的。大；就可以打压小的。大；身上的责任就大。我们有没有思考一个问题；台湾所有人就都愿意分裂？都不想统一？台湾人明知陈水扁至少是要搞‘准独立’，为什么还去选他？ <br />
要想解开僵局的唯一办法，就是双方先互相放弃对抗。 <br />
剩下的问题是；谁先作姿态？ <br />
答案只能是态度强硬的一方先做缓和姿态。因为；次强方做姿态所起作用是不明显的，甚至会使对方更咄咄逼人。至少次强方的心态是这样的。 <br />
说到回旋。两岸一度有过良性的接触。后来在一个问题上，被打了死结。从此两岸重新回到对抗并逐步升级。 <br />
其实；问题根结就是‘一个中国’的认识。 <br />
这个关于‘一个中国’，到底怎么回事？难道台湾当局不承认‘一个中国’？ <br />
事情是当初‘海基会’和‘海协会’曾达成一个书面的共识（俗称‘九，二共识’）。上面有‘双方承认一个中国’的字样。 <br />
这不是挺好的事情吗？双方都承认一个中国，往后事情不就好办了吗？ <br />
可后面就?治侍饬恕Ｎ颐钦獗咚担骸笆澜缰挥幸桓鲋泄??ㄍ迨侵泄?豢煞指畹囊徊糠郑?醒胝??诒本?Ｕ馐蔷偈拦?系氖率担?彩呛推浇饩鎏ㄍ逦侍獾那疤帷薄?<br />
台湾当局一下子就急了。说我们是承认只有一个中国，但不是你们说的意思。 <br />
我们政府于是说：签了字就应该遵守诺言。什么时候重开谈判，就以这个‘一个中国’为前提。否则就没有谈判基础。例如国台办新闻副局长李维一对中外媒体表示：“检验台湾当局是否有改善两岸关系的诚意，就在于是否接受一个中国原则”。 <br />
就这样；我们说‘一个中国’，他们说‘一国两边’，一直这么僵持了下来。 <br />
其实；‘一个中国’结症是在陈水扁，李登辉之前的蒋经国那里就遗留下来的。它的实质是所谓‘对等’谈判问题。最初我们的态度是不能‘对等’。后来对蒋经国派来的谈判代表说：“考虑到台湾的政治现实，照顾台湾当局关于平等谈判地位的要求，我们提出党和党对等谈判。但不存在我们和你们什么‘政府’的对等谈判”。对此蒋经国说到：“我们党内一部分人对中共所提条件持反对态度，理由是‘党对党’谈判，台湾人民会不赞成”。但蒋经国还是准备继续派谈判代表来北京。他认为；只要谈，就有希望。非常可惜的是蒋经国突然病逝。两岸和平的大好进程一下就断了。我们国家领导人对此极为痛心和惋惜。 <br />
