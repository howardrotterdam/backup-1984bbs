但它也不愿意台湾闹独立。因为会有战争的可能。给大陆口实，大陆急了真打了台湾，美国的那个‘民主模式’地区就保不住了。并且还会给美国带来很多其它麻烦。因此；美国是很希望台湾‘保持现状’，别无事生非。这也是真的。 <br />
说美国在我们武力收复台湾时一定会军事干预的人，也是对它的认识过于模式化了。 <br />
美国虽然一百个不愿意我们动武，但真到那一天；它是绝对不会用军事手段搅和进来。因为代价先不说（那等于全面战争了），而是它师出无名啊。为什么？是因为台湾不是一个独立国家（台湾自己说那不算）。别看当初北韩打南韩，北越打南越，美国都出兵了。但台湾和那二个例子差别大了去了。我们的百姓有很多不清楚的，美国自己可是清楚的很。别看它什么‘单边主义’，什么‘先发制人’。我们两岸的事情它可实在没有办法找到理由。 <br />
再则；想给中国玩真的，这样的国家世界上还没有。因为这根本不是打不打得赢的问题。越南战争；中国仅仅是给予越方物质援助，并派了部分工程兵和炮兵，就让‘头号帝国主义’背着5万多具尸体撤回去了。这就是远离本土到别国打仗的劣势。 <br />
对于中国，美国人有很清醒的认识。‘抗美援朝’，‘抗美援越’的记忆，对它来说是‘肚子疼自己知道’。对共党；台湾人更有清醒的认识。当有台湾的年轻人认为大陆永远不可能真的动武时；一位‘国军’退役将军说：“你们太不了解他们了，不说你们没有和他们打过仗；在他们‘三年灾害’饿死那么多人时，陈毅元帅仍然说‘当了裤子也还要搞原子弹’。” <br />
美国有时虚张声势，正是担心两岸开战。明眼人都知道怎么回事，包括其它国家。 <br />
日本在台湾问题上近一、二年来也跟着来劲。例如前几天美、日发表一个关于防务的联合声明、其中将台湾海峡纳入‘安保’范围。这是中国无法接受的。对美国，因为它是‘世界宪兵’，一贯搅和国际事物，人们至少有心理准备。但对日本；中国人就更不能容忍。 <br />
我想美国人的做法是没有办法的。它‘霸道’惯了，也有那个实力和二秆子劲。但日本如果还有一点清醒的话，奉劝它别来淌混水。如果说；美国和中国叫板；中国人会一跳三长高。那日本来和中国叫板；中国人则会一跳八长高。 <br />
但日本近来为什么这么跟我们中国来劲？要说原因；日本除了跟上述美国的想法近似，它还多一个‘台湾情结’。即台湾曾被它统治过半个世纪，后被中国收回。其中的酸楚尽可想象。 <br />
另外；日本认为如果中国过于‘霸道’了（指我们近年来的军备增长和对台湾的强硬），它比美国更受‘威胁’。道理很简单；它离我们太近，俗话说：首当其冲。再则，中、日间的仇恨可是比美国大得多。仇恨大了就有可能埋藏着复仇种子。 <br />
在不久前又听到那些‘军事专家’在放厥词；认为台湾是个‘岛链’，‘拿下’台湾后，就可以扩大‘防卫圈’和‘前进基地’。这使人不禁苦笑。这如同有人教唆安徽人去灭江苏人，而去获得海岸线和‘出海口’一般的荒唐。 <br />
我们民族越是内讧，我们的‘防卫圈’就越小。如果真的靠打台湾而获得‘防卫圈’，且不说我们自己两败俱伤，不知道多久才能缓过劲来。就是周遍国家和世界大国也会对我们增加十倍以上的政治防范及军事防范。那么到底是扩大，还是压缩了防卫空间？读者自己去判断。 <br />
是该反思我们整个民族‘防卫’问题的时候了。 <br />
1945年日本投降后，我们民族就开始陷入内战。内战结束后，我们又打了四场半（中苏算半场）外部战争。并且两岸也一直处于战争状态，使得双方都无暇顾及其它问题。等到当时的南越占了西沙的岛屿后，大陆才只得派兵去收复。在西沙海战期间，台湾是极为关注的。他们不仅是在精神上同我们完全站在一起，并在我海军的通行上给予方便，使得我们的增援舰艇能快速赶往火线。 <br />
再后来；南沙的形式使得我们又不得不派兵守卫。而先于我们守在那里的台湾守军，更是同我们大陆的官兵情同手足。双方相互支援谈水，蔬菜等。 <br />
仅说他们在较困难的环境下，替中国守住南沙最大的岛屿（太平岛），并一守就是50年这一点；我们是应该感谢的。至少应该感谢这些台湾守军的弟兄们。 <br />
说这些故事，是想让同胞们都想一想；我们有什么理由相互争斗，甚至残杀？这60年来我们做了多少让祖宗痛心的事情？还不够吗？还要闹下去吗？ <br />
大陆改革开放仅仅20余年，就取得令世界仰看的巨大经济成就。如果再加上台湾的经济实力，两岸在政治上，军事上都能坐在一条板凳上，即使没有我们旧概念中的统一形式，世人谁敢藐视？ <br />
实力，是一种无形的巨大威慑。无须张扬就能使得国泰民安。在两岸携手的情况下；我们也不搞大国沙文主义，也不搞霸权。仅仅是并肩站在那里，看有谁敢招呼都不打；就在南海挖矿，东海钻油？ <br />
20年取得的经济成就，是我们民族巨大创造力的展现。可千百年来我们同时向世人展示过内耗。一个能保持创造力，而逐步改掉内耗恶习的民族，前途是无可估量的。 <br />
如果我们两岸都有诚意，就没有解决不了的历史遗留问题。 <br />
这个诚意，不是文字游戏‘诚意’。而是把整个中华民族的利益置于两岸任何一方利益之上，经得住历史验证和后人评述的诚意。这就是本文要呼吁的核心理念。更应该是两岸领导人应具有的共同理念。 <br />
至此；我们可以得出这样的结论：两岸理想的统一方式，不是暂时无法实现的‘和平’统一，更不是武力统一。 <br />
应该是‘自然统一’。 <br />
自然统一；绝不是双方坐等，等它一百年。而是两岸都在发展经济，社会进步，民主进程上多下工夫。在此期间；能有多大合作可能，就尽力作好。 <br />
例如因台湾在世界上绝大多数国家没有使、领馆，当海外台胞每每遇到困难时；都是到当地中国使、领馆求助。而每当此时；我们的使、领馆人员都是尽最大可能去帮助他（她）们。比如在非洲国家政变，中东国家打仗时，都是我们的海外使、领馆及时解救。我在电视里看到那些被解救回来的台胞流着泪说：“还是祖国好啊”的时候，自己的眼泪也情不自禁。 <br />
大家都相互主动去做这样的事情；祖国统一就会早日到来。反之；多骂对方一句，尤其是多喊一句‘武力统一’，我们使、领馆人员所做善事的价值就少一分。 <br />
再比如台湾要求加入世界卫生组织，我们就一直阻挠。这使台湾民众非常反感。我想；在国际体育比赛时可以容许他们以‘中国台北’名义参加，为什么世界卫生组织的会议不能让他们以同样名义参加？ <br />
只要我们心平气和，就能做很多利于统一，利于民族的事情。 <br />
例如我说两岸的海军可以共同游弋南沙，巡防东海。有人相信吗？ <br />
事实上只要我们宣布对台湾放弃武力统一，这立即就可能成为现实，就这么简单。至于国际体育比赛联合组队这样的事情，不是更容易吗？还有‘三通’问题；想过为什么台湾迟迟不敢接受？等你不再说要打它的时候，它的戒心不就没有了吗？否则接受‘三通’在台湾看来；不简直是他们自己为我们进攻前做‘三通一平’准备吗？ <br />
如果我们在新闻联播中看到两岸海军并驾齐驱的画面，该会让多少人激动？即使‘愤青’们也会饱含热泪的。 <br />
统一防卫，当下不就是一种极具意义的统一形式吗？事实上在南沙；两岸军队已经统一防卫10余年了。可你们有谁愿意看到某一天在南沙他们自己打起来？而乐坏了旁人？ <br />
统一是可以一步一步的走，并且一个阶段一个阶段地分期实现。这就是‘自然统一’的实质。 <br />
说到我们民众们对台湾当前领导人的顾虑问题；仅想告诉大家不要把陈水扁等人太当回事；只要我们首先做出姿态，台湾民众，政界，甚至军队的反响会超出几乎所有人的想象。到那时；陈水扁等人稍有不顺民意之举，下台则是一天之内的事情。我们一定要明白这个道理。 <br />
主动示好，收回一些不利于统一的声明和言论；无论政府还是民众，无论是两岸的哪一边，都会有些痛苦和失面子。但再大的痛苦和面子，也大不过民族利益，大不过世界和平发展潮流。<br />
