													<h2>钓鱼岛的主权 从来没有什么“铁的证据”</h2>
						
						
																											<div id="postmessage_844263" class="t_msgfont">中国的老百姓义愤填膺，找出各种证据证明中国自古就发现钓鱼岛，并一直在经营，说这是铁的事实。我并不想去证明钓鱼岛是日本领土，但是我想指出，这些所谓钓鱼岛自古属于中国的铁证其实根本不经推敲。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;首先，按照国际法来说，一个岛屿是否属于某个国家，取决于谁最早并连续经营。你在地图上标出该岛屿，并不代表你就在经营该岛屿。你说自从明朝，我国就发现了钓鱼岛，“&nbsp;&nbsp;１５６２年明朝浙江提督胡宗宪编纂的《筹海图编》一书中的“沿海山沙图”，标明了中国福建省罗源县、宁德县沿海各岛，其中就有“钓鱼屿”、“黄尾山”和“赤屿”等岛屿”，这只能说明中国较早（或者勉强说最早）发现了钓鱼岛，并标注在地图上，可是你并没有去经营它。同样的道理，清朝时期人们把夏威夷标注在地图上，难道能说明清朝在经营夏威夷吗？这个论据在逻辑上无法成立。 (博讯 boxun.com)<br />
<br />
&nbsp; &nbsp; 同样的道理，你说慈禧太后把钓鱼岛赠给了盛宣怀，可是盛宣怀是否因此实地经营过钓鱼岛？如果这种赠予仅仅是字面上，而并没有在实质上改变钓鱼岛，那这种圣旨跟地图上的标注有什么不同？<br />
&nbsp; &nbsp; 如果哪怕你在钓鱼岛设一个界碑，或者灯塔，并明确标注大清国，中华民国，或者中华人民共和国领岛，那这样的行为才能算得上实质经营该岛屿。很明显，没有任何证据证明中国人这样做过。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 其次，中国政府的态度完全是心虚的表现。中国政府一方面明确声称钓鱼岛是中国领土，另一方面又要求跟日本人共同开发钓鱼岛，荒谬绝伦。既然钓鱼岛是本国领土，就不需要作出这种让步，而应该理直气壮。如果钓鱼岛不是本国领土，就不应该无理取闹，要求共同开发。 <br />
&nbsp; &nbsp; 每次中国船只进入钓鱼岛，日本警戒船就会用喇叭高喊：你们已经进入了日本领海，请离开。可是中国船只的回答是：这是中国领海，请不要干扰我们。这也是心虚的表现，按照中国人的说法，只要日本人不干扰我们，就可以进入中国领海吗？<br />
&nbsp; &nbsp; 再次，如果我是一个日本人，我会觉得中国人完全是在无理取闹。为什么呢？因为钓鱼岛在第二次世界大战结束后长期被美国占领一直延续到1972年。既然钓鱼岛是你中国领土，为什么美国占领的时候你不去索要，你不去向美国政府提交抗议，反而是在美国把钓鱼岛移交给日本的时候，你才去抗议和索要？你可以说台湾政府跟美国关系友好，那为什么在那个时候，大陆的中华人民共和国政府从来没有宣称钓鱼岛是中国领土？反而是在1950年代的《人民日报》里面明确使用“尖阁列岛”这样的名称？如果你一直在经营钓鱼岛，你怎么会使用“尖阁列岛”？更有甚者，1974年的中华人民共和国地图明确把钓鱼岛划在国界之外，这难道不是你的真心表白吗？<br />
&nbsp; &nbsp; 最重要的一点是，既然你认为钓鱼岛是你的领土，你为什么不派遣军舰和飞机巡逻？你的舰队在哪里？你为什么不行驶主权？不去实际控制岛屿，呆在家里放空炮有什么意义？<br />
&nbsp; &nbsp; 总之，没有任何确凿证据证明钓鱼岛是中国领土，反倒是有大量证据证明中国政府在钓鱼岛问题上是心虚的，无理取闹的。中国人民应该敢于去问责政府究竟为什么这么心虚，这么软弱，要么政府欺骗了人民，要么政府没有履行保卫国家的责任。钓鱼岛的现状，首先应该责怪于海峡两岸的中国政府，而不应该问责于日本。 [博讯来稿] (博讯 boxun.com)</div>

							
							
							
							
