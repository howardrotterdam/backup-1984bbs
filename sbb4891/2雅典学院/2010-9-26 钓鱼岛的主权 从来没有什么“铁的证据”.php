													<h2>从历史上说</h2>
						
						
																											<div id="postmessage_844962" class="t_msgfont">1、《马关条约》与尖阁列岛无关。&nbsp; &nbsp; <br />
日本是在1895年1月14日的日本内阁会议中把尖阁列岛纳入国土，3个月后的1895年4月17日才和满清签定马关条约。条约签订后，满清也没有声明尖阁列岛主权仍属中国。<br />
<br />
2、日本人最早到尖阁列岛开垦<br />
日本人古贺辰四郎从1895年就率众到尖阁列岛开垦，到1909年移民已有90户共248人,开垦规模还不小,现在岛上仍有古贺家族的遗迹。<br />
<br />
3、1949年后北京中共政权对尖阁列岛之定位<br />
1953年1月8日《人民日报》，在刊载的《琉球群岛人民反对美国占领的斗争》专文中清楚的写着“琉群岛包括尖阁诸岛”。<br />
1958年11月北京地图出版社编辑部编印的地图《日本之部》，将尖阁列岛划入日本领土之内。 <br />
1959年商务印书馆出版《台湾地理》，明确将台湾北界定在彭佳屿。<br />
<br />
4、1949年后台湾国民党政权对尖阁列岛的定位<br />
1955年台湾公文书中清楚将将恶劣大松标示为琉球群岛的一部分。<br />
1960年，中华民国内政部制作《台湾省5市16县详图》，明记台湾附属岛屿76座，分布范围从a春七星岩到基隆彭佳屿，未记载尖阁列岛。 <br />
1965年10月由国防研究院与中国地理学研究所合编出版的《世界地图集》第一册东亚诸国中，将钓鱼台列屿划为日本领土并写成日本名尖阁群岛 <br />
1965年11月台湾省政府编印的《台湾省地方自治志要》将台湾省极北界定为彭佳屿<br />
1968年10月台湾印行的《中华民国年鉴》以台湾极北为彭佳屿。<br />
1970年以前台湾国立编译馆编印之国民中学地理教科书将钓鱼台划入日本领土，名称也使用日方使用的尖阁群岛。</div>

							
							
							
							
