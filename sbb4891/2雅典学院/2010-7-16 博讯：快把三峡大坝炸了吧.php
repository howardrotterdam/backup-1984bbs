&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;我们现在已经成为世界建坝最多的国家.已达8.6万.也是建坝最烂的国家.一条300多公里的黑河,我们可以建将近98座大坝!这不是天方夜谈,是利益分割的荒唐!为了阻止沙石下泻,现在又在三峡上游大渡河修建电站,据说,怒江也要修坝了.雅鲁藏布江也要修了,我们国家将不再有一条自然河.黄河现在几成季节河,河滩上甚至可以种庄稼.沿途烂建的水库大坝,把这条母亲河切割的七另八落.建大坝已对生态造成了严重的破坏.河流污染加剧,大坝也是重要原因之一.眼下正闹水灾,重庆的水深前所未有,已达三米.以前修三门峡的时候,移民也有数十万,说是建了三门峡,免除了下游水患,可事实是,下游是不闹水灾了,由于泥沙淤积不下泻,将上游的河道淤高,如渭河,河床淤的太高,河床变浅,形成水灾上移,小水大灾,古来富庶的800里秦川早已风光不再.<br />
&nbsp; &nbsp; <br />
