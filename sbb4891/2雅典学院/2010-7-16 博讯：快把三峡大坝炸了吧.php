&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;大坝注水,大约400亿立方.蓄水后很快发生了开县井喷事故,那起事故伤亡人数我记不清了反正至少也有百十号人吧.400亿立方水,相当400亿吨水体,压在库区.势必会造成水下地表应力的改变.地壳要寻找新的平衡,有些板块相互挤压,就会出现新的缝隙,开县天然气的井喷就是这样发生的.再有是汶川大地震,有学者指出,该地震的发生,三峡也难逃干系.也是因为三峡库区蓄水量不断发生变化,将地下能量挤到龙门山断裂带,导致能量释放而发生了地震.当初建大坝,振振有辞,说建了大坝,下游可以永远免除水患,当时就有学者反对,说雨季注入长江的水主要来自中下游,汉江.枝江,大坝防洪能力实在有限,事实证明,人家说的对,现在长江水患,不仅下游不断,连上游也不能免啦,情况比建坝之前更严重.这一次次大洪水,有多少人死于非命?眼下重庆到武汉到安徽上游下游洪水一起泛滥，248万人受灾。请问，大坝的防洪功能那里去了? (博讯 boxun.com)<br />
<br />
&nbsp; &nbsp; <br />
