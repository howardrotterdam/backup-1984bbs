　　----------------------<br />
　　<br />
　　作为蜜月旅行的第二部分，我们游览了苏州，回上海之后，我发现身上出了疹子。我试用各种油膏檫抹，不但无效，而且越来越糟。后来，突然发现腿上也出现疹块，手腕按脉处也出现两块红疤。它们虽不痒，但看起来很不好，我很发愁，我一生从未得过这种病。<br />
　　<br />
　　介石带我去看他的朋友李大夫。他是德国留学生，专精细菌学和性病。李大夫取了介石和我的血做瓦塞尔曼氏反应检查（梅毒血清诊断法）。等了令人心烦的日子，这位血清专家宣布我们的血有阳性反应。<br />
　　<br />
　　我一听到这个坏消息，发了疯似的，立刻从座位上跳了起来，冲出诊所，坐上一辆出租汽车到我母亲家。<br />
　　<br />
　　“噢，妈妈。”我哭诉着，“我得了花柳病。是大夫告诉我的。看看我这些毛病。”<br />
　　<br />
　　不到半小时，介石来了，解释说这病是轻度的，用六零六针药可以痊愈，这是他自己的旧毛病，传给我了。<br />
　　<br />
　　母亲痛骂了介石半小时。他承认了他的“罪恶”。<br />
　　<br />
　　“我再也不同你在一起了。”我哭喊着，“你是个坏东西，我要跟你离婚。”<br />
　　<br />
　　“我怎样才能使你相信我要真的悔罪呢？”他哀求着，低头站在那里，表情非常严肃阴沉，接着说：“只原谅我这一次，回到我的身边来。我向你发誓，为了悔过，永远不再沾一点酒。”<br />
　　<br />
　　母亲拉着我的手说：“你要同他去大夫那里，赶紧去治，不可耽搁，最要紧的是把那个病从你的血中清洗干净。”<br />
　　<br />
　　于是，那天黄昏，介石再带我去李大夫诊所治疗。在打针前，李大夫对介石说：“这是个不大好谈的话题，所以请让我和你的夫人单独谈谈，你可不可以在候诊室等一下？”介石出去以后，李大夫向我作了六零六注射，并说：“你打十次针，就可痊愈。我现在要坦白告诉你，淋病细菌已进入你的身体，说确切点，就是你的输卵巢，这可能使你不能怀孕。但是你的病是轻度的，只要继续坚持治疗，就不必担心。”<br />
　　<br />
　　事毕，我走进候诊室轮到介石进入诊疗室了。他打过针后，李大夫告诉他：“你在结婚前，本应先完成以前的治疗，但你没有等待充分的时间以完全治愈，因而传染了你的夫人。从现在起，你必须继续坚持治疗以便康复。你原已患有副睾炎，这使你不能生育。今后你恐怕不可能再生育孩子了。”<br />
　　<br />
　　-------------------------------<br />
　　<br />
　　所以，蒋介石不能生育，原因就来自于他患有梅毒，这与他从日本归国后，在上海如浪人般的浪荡生活脱不了干系。说这又引出另一个问题，传说蒋纬国并不是蒋介石的亲生儿子，而是与蒋在日本时，同为留学生的载季陶所生。其生母是一个日本护士，名为重松金子。至于载季陶的儿子为什么最后归蒋介石领养，这在历史上一直众说纷云。有人说，这个重松金子曾经同时与两人「交好」，至生子无法办别为何人之子，所以约定由蒋介石抚养。又有一说是因为载季陶当时已有家室，重松金子为载之秘密情人，与蒋并无鱼水之欢。只是载怕私生子之事将影响其家室，遂交蒋抚养。<br />
　　<br />
　　不过到底为什么载季陶的儿子会交由蒋介石抚养，随着当世人一一死去，已经不解的谜团。唯一留下的证据，就是蒋纬国自己在晚年的传记中，承认了自己的生父应是载季陶。不过就是因为如此，生为养子的蒋纬国一直受到蒋经国的排挤，在台湾时与权倾一直时特务头子蒋经国不能相提并论，一直任陆军中将十余年而无法升迁，其原来的手上已有升官至上将者，而蒋纬国却仍抑郁不伸而无实权。最后连蒋宋美龄都于心不忍，召来蒋经国，对他说不要让自己的弟弟一直喝「中将汤」，才让蒋纬国在退役前如愿升上上将军职。</div>

							
							
							
							
