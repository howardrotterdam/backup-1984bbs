													<h2>李普曼：《十月围城》传递了很贱的价值观</h2>
						
						
																											<div id="postmessage_441880" class="t_msgfont">《十月围城》传递了很贱的价值观<br />
On 01/05/2010, In 影・音・书, By 李普曼<br />
<a href="http://www.lipuman.com/archives/october-besieged-city-to-pass-the-values-of-a-very-cheap/" target="_blank">http://www.lipuman.com/archives/ ... es-of-a-very-cheap/</a><br />
 <br />
真喜欢韩寒的这个句式，难免经常拿来做二道贩子重新演绎。本不想写这个电影了，毕竟都已经过气了。可是元旦前，阻止了朋友去看这部电影，朋友很好奇，到底是如何很贱的价值观，要我写出来。我答应了，不好不完成。<br />
<br />
忽然想到，也许《十月围城》传递的价值观或许倒能够解释当下普通民众对重庆打黑的一些看法。就在我准备写这样一篇博客的时候，一则新闻先进入了我的脑袋：重庆官方：近九成市民认为打黑增加了安全感。我不知道这个数据到底是怎么得出来的，但是我相信，即使倒不了九成，至少也有大多数的市民对打黑是持赞同和支持态度的。虽然，重庆在打黑过程中，置程序和法律与不顾的事情比比皆是（石扉客在他的博客里多有分析，只看他一家博客就能找到很全的相关信息。），但是大部分老百姓对这些都视而不见。因为在他们心目中，黑老大、黑社会是一个人人的而诛之的对象，现在重庆市政府打击他们，当然就是好事。在这个正义的前提下，任何的程序不公，任何的违背法律精神的瑕疵和缺陷，都是可以被忽视，可以被原谅，可以被接受的。<br />
<br />
《十月围城》不正是传递了这样的一种价值观吗？电影一开始，就创造出了一个救世主的形象：孙中山。毫无前因，没有后果，反正在1905年的中国，孙中山就是中国的希望，就是四万万同胞的未来。接下里，借梁家辉饰演的革命鼓动家之口，电影树立了一个光辉且伟大的目标：革命。而孙中山又将“革命”不断的神圣化、浪漫化：欲求文明之幸福，不得不经文明之痛苦，而这痛苦就是革命！<br />
<br />
文明之幸福是什么样的？被囚禁中的梁家辉义正言辞的对学生胡军说，就是人人生而平等――这也是我们听的耳朵都起了茧子的一个美好的口号和未来，籍着这个口号，成全了多少肮脏的政治体制和政府。<br />
<br />
故事，就是围绕着这一个救世主，这一个口号和目标进行的。而也就是在这个目标下，口口声声说人人生而平等的梁家辉，将李宇春、谢霆锋、巴特尔等人，送到前面去当炮灰。梁家辉从没有问过自己，凭什么？凭什么孙中山的命，就比这些炮灰的命更值钱？革命是需要流血的、需要牺牲的，为什么孙中山不能像谭嗣同一样流自己的血？牺自己的牲？就当谢霆锋拉着假的孙中山――那些炮灰当然被蒙在鼓里――奔跑的时候，当李宇春、黎明等人失去生命的时候，孙中山却躲在一个屋子里，指点江山，豪情万丈。<br />
<br />
为了一个看似光明的目标和一个看似可以委以重托的救世主――在1905年的中国，这两点实在牵强――是不是就可以牺牲一个人甚至是几个人的生命？尤其还是在不告知对方实情的情况下。为了所谓的四万万人的幸福，是不是就可以牺牲个人的幸福？<br />
<br />
电影中所流露出来的价值观，就是这样的价值观：答案是肯定的。为了几个人口中的四万万人的幸福，几个人的幸福又算什么？为了中国的未来，几个人的未来有算什么？为了保护救世主，普通人做炮灰是他们的荣耀――电影终结时候，不断以字幕形式闪现的各地起义形势图，就是为这个荣耀做背书――他们的生命，换来了燎原之火。<br />
<br />
我承认这种价值观确实在中国很有市场，但是同时我也认为这就是一种很贱的价值观。相似的历史已经在几十年里一再上演，所谓的共和也在名义上成立，可是，四万万人的幸福真的来了吗？还是那句话：多少流氓，假借着共和之名行恶。电影中胡军对梁家辉说：如果让你们成功了，国家必亡。看现在，想当时，真是一言中的啊。<br />
<br />
写到这里，我再次想提起当时看《2012》时候的一个细节：当时奥巴马决定不上空军一号飞往中国，而副总统的直升机又在半途坠毁。这时候那个负责人很沉着的说了一句：还好，我们还有宪法。<br />
<br />
你也许会说，这不过是美国电影中经常传递的主旋律罢了，也只是一个美好的虚幻镜像而已。可是，主旋律和价值观一样是分贵贱的。而且，影视作品对我们看待世界和思考问题角度的影响是潜移默化的――多少人的伪清朝历史都是从清宫戏中积累的呢？――美国在这种价值观下，就是能建立起宪政、建立起法制来。而我们在《十月围城》的价值观下，才会坚定不移的将自己的未来寄托在某个救世主身上，而很少去追求自己的幸福；我们在这种价值观的影响下，也才会认为，只要目标是伟大的，即使程序错的再离谱，也都是可以被肯定，可以被弘扬的；我们也许甚至会嘲笑美国人：那帮傻逼，既然都认为辛普森杀了他的妻子，怎么能因为一个小程序的问题，就放了他呢？</div>

							
							
							
							
