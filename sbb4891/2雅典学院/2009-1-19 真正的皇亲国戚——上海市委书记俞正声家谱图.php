													<h2>真正的皇亲国戚――上海市委书记俞正声家谱图</h2>
						
						
																											<div id="postmessage_23027" class="t_msgfont">这个家族说起来还是和曾国藩有关的，曾的孙女，嫁给浙人俞明震，俞明震何许人也？俞明震（1860-1918），字恪士，号孤庵，晚清知名于诗界、教育界、政界。甲午战争时，曾协助唐景崧据守台湾。 <br />
<br />
　　俞明震还曾担任厘捐总局局长，甘肃省学台、藩台等职。也曾南京江南水师学堂督办，也即校长。 <br />
<br />
　　1898年，俞明震的学生当中，有一个人在他的帮助下到了现在的日本北九州的东北大学医学院（原来的仙台医学专科学校），这位学生命叫周树人。《鲁迅日记》中多次提到&quot;恪士师&quot;，就是俞明震。鲁迅还曾在《琐记》一文中，以亲切的笔致描述过后来送他出国留学的&quot;恩师&quot;，当然了，这只是这个家族有关的人当中的一个小小的插曲而已。 <br />
<br />
　　俞明震长子俞大纯（又名俞大线？），即俞启威之父。俞大纯曾留学日本、德国。归国后曾任北洋军阀政府工艺局长、国民党政府交通部陇海铁路局局长等职。俞大纯膝下四子两女。长子俞启孝生于北京，留学美国，回国后在天津当教授；老二俞启信生于德国，专攻化学；老三即俞启威（化名黄敬），生于北京，人称&quot;三少爷&quot;；老四俞启忠，学农，从美国回国后在北京当教授。老大，老二，老四兼娶了名门之后，唯有三少爷行为放荡不羁，家人也无奈。 <br />
<br />
　　不想，一日三少爷启威与一名叫李云鹤的山东女子结婚，后来这个女子改名叫江青…………。 <br />
<br />
　　大姐俞珊，生于日本，演员。小妹俞瑾，一生从医。 <br />
<br />
　　还是说说三少爷俞启威吧，他后来化名黄敬，是毛泽东夫人江青前夫，也就是现在的中中/共央政治局委员俞正声的老爸，是解放后继黄克诚之后，为天津市首任文官市长和国务院第一机械部部长。1958年，俞启威因被毛泽东当众训斥，惊吓过度成精神病，于11月去世.<br />
<br />
　　顺便说一句，俞正声的老哥俞*声也很有名，不过不方便在这儿多说，有兴趣的老大可以自己google去。 <br />
<br />
　　再说说老大俞大纯，有个妹妹嫁给陈三立，这个陈三立就是维新派主将陈宝箴的老大，他有3个儿子，老大著名画家陈衡恪，老三的名字叫做陈寅恪。 <br />
<br />
　　俞明震共有三兄弟，他居长。 <br />
<br />
　　俞明震的三弟有个儿子，美国留学，后来到了台湾发展，名字叫俞大维，想当初金门823炮战，在岛上的两个一个是胡琏，另外一个人就是“国防部长”俞大维。看到这里可能有人要问，胡琏是黄埔四期，当年一人独守夔门，与日军激战半月不退，50年代金门一战，更是国军之中流砥柱，俞大维何德何能，反居胡琏之上？也许这就是家族的的原因吧…………穷人家的孩子要成功总是要付出更大的心血的。这也是我有时候有些比较喜欢阿扁，看不起连战的原因了。闲话不多说，俞大维的儿子叫俞杨，娶了个老婆名字叫蒋孝章，当然亲家翁就是蒋经国了。他们生了个儿子叫俞祖声（和俞正声一辈的）。这是这个家族在台湾的布局。 <br />
<br />
　　而俞大维之妹叫俞大彩，他的老公叫做傅斯年。当年这厮可是在老蒋面前撬二郎腿的脚色哦。傅斯年曾任北京大学校长。1950年1月起,任台湾大学校长。 <br />
<br />
　　回过头来再说说俞明震的小姑的女儿。也就是曾国藩的曾孙女，她的名字叫曾宪植，她的丈夫名叫叶剑英。换句话说，俞正声的父亲俞启威（黄敬）也得管叶帅叫姑父。 <br />
<br />
　　俞正声的母亲叫范瑾，曾任北京市委常委兼北京市副市长、《北京日报》社社长，而俞正声的舅舅则是范文澜。 <br />
<br />
　　俞正声之妻张志凯，老爸张震寰，前解放军总参装备部副部长，国防科学技术委员会副秘书长，国防工科委主任。少将军衔。<br />
<br />
　　1952年秋天，重工业部撤销，分别建立第一机械工业部、第二机械工业部、冶金工业部等。汽车工业归一机部领导。一机部部长即是黄敬，一机部建立汽车工业管理局，张逢时任局长。 <br />
<br />
　　1953年7月15日，位于长春的一汽举行奠基典礼，参加典礼的有东北人民政府第一副主席林枫、第一机械工业部部长黄敬等。会上黄敬讲话，李岚青、王恩魁等六名年轻共产党员，将刻有毛主席题词&quot;第一汽车制造厂奠基纪念&quot;的汉白玉基石，放置在厂区中心广场基座上。可见对李岚青这些老机械、老汽车来说，俞正声的父亲黄敬一直是他们敬重的老领导。 <br />
<br />
　　综上所述，这个家族关系要是写一本书绝不为过。 <br />
