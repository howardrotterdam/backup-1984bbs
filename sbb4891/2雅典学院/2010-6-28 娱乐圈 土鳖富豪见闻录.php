<br />
<br />
<br />
今天早上我爸从上海s啊b会回来。然后就接到一老同学的电话，问他去不去中山玩。他就叫上我一块去。于是我也就跟着去混饭吃鸟。<br />
<br />
第一站先是去一个造变压器的工厂那里。去到工厂时，看到院子里停了一辆白色的BMW七系，一穿着绿色国民党制服的光头老家伙和一个高挑的模特身材的浓妆女子正从车上走下来。后来得知，这个老头是演某国民党大员的特型演员，而那个浓妆女子，则是某选美大赛的十强。<br />
<br />
（插一句：本来还有个扮演老毛的特型演员要来的，可是他临时要陪省公安厅厅长，所以就没来了。）<br />
<br />
变压器厂老板带我们到处参观了一下，主要就是指他种的那些名贵树木给我们看。他的别墅就在工厂里面。这个占地颇大的工厂绿树成荫，还有一个小湖，湖上有小亭，湖里有几千条锦鲤。老板就喜欢搞这些东西。<br />
<br />
在工厂里转来一圈之后，我们就去吃饭。一行人在一个类似农家乐的地方吃了个饭。看着那一锅的野生鸟类我就有点负罪感。可是～又不是我点的，如果我不吃，那么也是浪费，那就吃呗～～～～这一饭桌上的人基本上不是亿万富豪就是国企老总，不是公安局长就是国安局的，后来在旁边包厢里吃饭的镇长和他的朋友也闻风过来，向“国民党大员”轮流敬酒。<br />
<br />
吃完饭，我们就开车去一个电子企业的老板家里。这个老板的家占地3800平米，其中建筑占地1700平米，三层再加地下室，加起来建筑面积该有好多个千的平米吧～～～他的别墅前后和旁边都有院子，几个鱼池，后院里还有一条弯弯曲曲的小溪，院子里面随便一棵小树苗就要十多万，一棵大树几十万。他家还有一个养龟池，养了20多只金钱龟，他随便指着一只大的是：这个值六万，又指着一小的说：这个值两万，哇靠～那20个龟龟加起来的价值也是好多辆大奔了吧。<br />
<br />
老板家里有一个房间的古董，什么唐三彩什么花瓶象牙一大堆，还有唐伯虎和文征明的画，不过不知道是否真迹。去参观老板的家的还有一个江苏来的研究陶器的高级工程师，七十多岁的一老头。老板请他鉴定他家收藏到那六十个紫砂壶。老头儿把脸上的黑框眼镜摘掉，然后换上一副金边眼镜，把紫砂壶们一个一个摸过看过，最后得出结论：“这些都不错，只不过是仿的”。<br />
