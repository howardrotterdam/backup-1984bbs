<br />
自从&quot;使命&quot;这两个字浮现在脑海之后，本村桑决定透过电视传播媒体向一般社会大众表达自己的主张，让社会大众更加了解犯罪被害者的心境以及犯罪被害者在司法前受到的不平等待遇。 <br />
