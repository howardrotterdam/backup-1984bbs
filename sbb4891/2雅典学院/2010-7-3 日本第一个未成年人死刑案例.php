2002年3月14日，广岛高等裁判所将检察官对被告求处极邢的控诉驳回。 <br />
<br />
理由是：“犯人当时才刚满18岁又一个月，思想尚未成熟，顾及被告未来还有无限的可能性。 对于将来，不能论定犯人完全没有更生的机率，所以驳回检方死刑的控诉，维持无期徒刑的判决。” <br />
<br />
二审虽然又被法院驳回，可是检察官还是不屈不挠，决定继续上诉最高裁判所(法院)。 <br />
<br />
检察官得知被告在狱中曾经寄出几封信件给外面的友人。 于是挨家挨户的查访，终于探访到寄出信件的收件人，并且得到收件人(被告友人)的同意，取得被告亲笔书写的信件。 <br />
<br />
对于自己犯下的强奸杀人罪，被告福田孝行是这么写的： <br />
<br />
「不过就是一只公狗走在路上，碰巧遇到一只可爱的母狗，公狗自然而然的就骑上去了......这样也有罪吗！？」 <br />
<br />
被告福田孝行因为法律的保障，国家有义务提供替他辩护的律师，费用由国家全数支出。 <br />
<br />
特别值得提出的是，这次福田被告的辩护律师并非由国家提供，而是民间的律师团体自愿出任。本案上诉到最高法院时，被告福田孝行的辩护律师由原来的两人(自愿担任)增加为二十一位，规模之大， 堪称世纪辩护律师团。 <br />
<br />
这些辩护团律师成员们正是所谓的人权拥护者，以废除死刑为最大的使命以及任务。 <br />
<br />
至此，本来是一场单纯的凶杀案的审判 ，却被这群赞成废除死刑的人权派律师们当成表演舞台， 开始他们一幕幕卑劣可耻的表演活动。 <br />
<br />
第一、二审时，被告福田对于犯行的经过以及对受害人的杀意完全没有 否认也没有争论的地方。可是到了最高法院开庭公审，福田被告的辩护律 师从原本的两人改成二十一位辩护律师团之后，突然全盘否定之前的供述 。 <br />
<br />
辩护团的主任律师 －安田好弘指出，在他接见被告时，被告向他宣称 当时他对受害人本村 弥生以及本村 夕夏并无杀意。之所以没有在一、二 审的时候提出，是因为被告当时的主张并没有被采纳。 <br />
<br />
世纪辩护团提出以下的主张： <br />
<br />
被告福田的母亲是自杀身亡，被告因为渴望母爱，希望被母亲拥抱的j望过于强烈，才会在见到被害人时情不自禁的抱紧被害人，最后造成被害人死亡的遗憾。被告并非是强奸目的而侵入民宅，而是想求取失去的母爱。 <br />
<br />
至于被害人死后还对被害人尸奸的行为，世纪辩护团的律师是这样辩解的： <br />
<br />
因为被告福田认为，只要将精子送入被害人的体内，被害人就会起死回生。所以死后对遗体的性行为并非污辱遗体，而是一种起死回生的仪式。至于用绳索勒毙夕夏小妹妹也不是心存杀意。因为夕夏妹妹一直哭泣，福田被告想让夕夏妹妹停止哭泣，所以在她的脖子上绑上蝴蝶结而已。」 <br />
<br />
世纪辩护团律师的结论是： <br />
<br />
被告并非故意强奸杀人而是伤害致死。检察官那方因为想让被告被处死刑，所以把被告塑造成十恶不赦的形象。 <br />
<br />
还好，检方提供福田被告寄给友人的信件做为证据。对照一审跟二审法官认为「被告未来仍然有无限的可能性以及被告已经有悔改之意的说词」与福田被告寄出信件的内容，无疑是一大讽刺。 <br />
<br />
2008年4月22日，法官对被告一方的辩护主张全面否定，宣判福田被告因恶行重大处以死刑。距离命案发生时已经经过九年的岁月。 <br />
<br />
死刑宣判后的记者会上，本村先生并没有任何&quot;胜利&quot;的喜悦。 <br />
<br />
2002年审下了无期徒刑的判决时，本村先生曾经这样说过： <br />
<br />
「死刑的意义在于，让一个犯了杀人罪的犯人，诚实的面对自己犯下的错误 ，打从心里反省自己的误行，决心将自己剩余的人生用来赎罪并对社会做有意义的奉献。一个本来十恶不赦的坏蛋，最后可能会脱胎换骨变成真诚努力的善人。 <br />
<br />
可是，国家社会却要夺去这位，已经重生的&quot;善人&quot;的性命。很残忍 ，很冷酷，是不是？是的！无情的夺取他人宝贵的生命的确是很残忍的一件事。相对的，这个时候犯人才会真切的体会到，被自己残忍杀害的人，他们的生命也是这样的无价。 <br />
<br />
死刑存在的意义不是报复手段，而是让犯人可以诚实面对自己所犯的恶行的方式。」 <br />
<br />
本村先生七年前的主张，竟然在福田身上应验。一、二审判无期徒刑时， 福田本身也很清楚，大概七、八年之后就可以假释出狱。 <br />
<br />
写给友人的信件当中，充满了侮辱被害人以及其家属的言论， <br />
<br />
其中还有藐视司法的部分。 <br />
<br />
他写着：「这世界终究是由恶人获胜的～七、八年之后，等我出狱时，你们要举办盛大的party欢迎我啊～」 <br />
<br />
你完全没有办法感受犯人的悔意。 <br />
<br />
可是在下了死刑的判决之后，福田被告终于意识到自己犯下的罪的严重性， <br />
<br />
开始写信给遗族表达自己的忏悔。 <br />
<br />
很遗憾的，有些人只有在自己的生命受到威胁时，才会了解生命的尊严及意义。 <br />
