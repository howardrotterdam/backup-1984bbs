<br />
是的，你没有看错，当时主审的法官确实是这麽说的。因为被害者的遗照会影响加害者的心理情绪。 <br />
<br />
开庭时，犯人 福田 孝行 穿着拖鞋进入法庭，辩护律师推推他的手示意， 福田这才对着被害人家属的方向鞠躬，说了一句：「真是对不起，我做了无法宽恕的事。」 <br />
<br />
这句&quot;对不起&quot;，成为之后法官认定犯人&quot;已经有悔改意思&quot;的参考。 <br />
<br />
杀了两个人，只要事后表现出&quot;我很抱歉&quot;的样子，就代表有悔改，然后就可以得到宽恕。 <br />
<br />
本村 洋 先生不断的跟法官抗议，最后，法官准许他带遗照进去，条件是必须用黑布将照片盖住才可以。当时一审下的判决是 － 无期徒刑。跟台湾类似的是，日本并没有真的无期徒刑。尤其当时的少年身上有着少年法保护，顶多关个七、八年（表现良好的话）就可以出狱。 <br />
<br />
当时被告的辩护律师，竟然在法官下了无期徒刑的判决时， 对着旁听席的被害家属，比了一个&quot;胜利&quot;的手势。 <br />
<br />
本村 洋 先生在判决之后招开记者会，他是这么说的： <br />
<br />
「我对司法很绝望。原来司法保护的是加害人的权益，司法重视的是加害人的人权。被害者的人权在哪儿？被害家属的权益在哪儿！？如果司法的判决就是这样，那不如现在就把犯人放出来好了，我会亲手杀了他！！」 <br />
<br />
记者会结束之后，本村先生走进担任本命桉的检察官办公室。检察官吉田先生戴着银框眼镜，个性沉稳内敛。 <br />
<br />
平时给人一种酷酷的感觉的吉田先生，突然以愤怒颤抖的声音对着本村先生说出自己的想法，这突然的举动 让本村先生屏息。 <br />
