当天晚上十点半，本村先生准时的出现在「ニュ`スステ`ション」的节目上。 <br />
<br />
脸上的表情已经没有中午开记者会时的激动，或许他已经意识到 自己的「使命」，所以他冷静客观的对着全国观众作诉求。 <br />
