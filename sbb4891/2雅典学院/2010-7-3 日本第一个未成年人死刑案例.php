<br />
在回应的11天后，小m总理因为脑梗塞紧急送医、不幸于5 月14日逝世 。可是在他过世前两天，「犯罪被害者保护法」、「改正刑事诉讼法」、 「改正检察审嘶岱ā拐馊个法案在国会全数通过。 <br />
