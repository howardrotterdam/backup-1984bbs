这是一个日本第一个未成年人被判死刑的案例 <br />
<br />
1999年4月14日，日本的山口县光市发生一件残忍的凶杀案。当时23岁的本村 洋先生于晚间七点左右下班返家，发现大门没有锁。 <br />
<br />
进了家门之后，四处不见妻子跟11个月大的女儿夕夏的踪影。家里一片凌乱，不安的本村洋先生开始在不算大的家里找寻妻女的踪迹。最后在收纳棉被的柜子里面，发现妻子半裸而且已经变僵硬的尸体。 <br />
<br />
本村洋先生马上报警，警察抵达之后，在收纳柜最上层的地方，发现用塑胶袋包着，当时才11个月大的夕夏妹妹的尸体。 <br />
<br />
1999年4月18日，警方逮捕当时刚满18岁一个月的少年（日本法律规定20周岁为成年）。根据犯人的供述，他于4月14日当天下午两点左右，乔装成排水管检查的工人，按门铃顺利进入被害人家中。 <br />
<br />
目的只有一个－强奸被害人。少年将本村弥生压在身体下面，可是遭到被害人激烈的反抗。少年于是动手掐死被害人，被害人弥生窒息死后，加害者的少年用事先准备好的胶带将被害人双手绑， 并在口鼻处也黏上胶带（预防被害人&quot;万一&quot;又苏醒）， 对死去的被害人进行尸奸。 <br />
<br />
当时11的月的婴儿夕夏一直在妈妈的旁边哭泣不休，少年将婴儿抛往别处，可是婴儿还是挣扎哭着，往已死去的母亲遗体处爬去。 <br />
<br />
兽性大发的少年怕婴儿的哭声引起邻人的注意而坏了他的好事，于是将哭闹不止的夕夏从母亲遗体旁边拉开，重摔地面数次之后再用绳索勒毙。 <br />
<br />
虽然加害的少年当时未满二十岁，可是所犯的案情残忍重大，山口县的少年法庭决议将全桉移交山口地检署审理。第一次开审议庭时，本村洋 先生抱着妻女的遗照出庭，却被法官阻止。 <br />
