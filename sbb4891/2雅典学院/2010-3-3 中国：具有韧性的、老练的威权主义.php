													<h2>中国：具有韧性的、老练的威权主义</h2>
						
						
																											<div id="postmessage_500059" class="t_msgfont"><a href="http://docs.google.com/View?id=ddxmkbw_34crv2qv9m" target="_blank">http://docs.google.com/View?id=ddxmkbw_34crv2qv9m</a><br />
来源：自由之家、自由欧洲、自由亚洲联合发布的《Undermining Democracy――21 Century Authoritarians》报告里面的中国部分<br />
<br />
作者：Joshua Kurlantzick，Perry Link<br />
发表时间：2009-06-04<br />
译文发表时间：2010-03-02<br />
译者：推特ID：@Freeman7777，@hsinwang1982，@jiangge09<br />
校订：@Freeman7777、@jiangge09<br />
<br />
中国共产党的领导人看来已经欣然接受了软实力的概念，它正在成为其发表关于中国在世界角色的言论时的核心。而仅在5年前，中国官方和学界还否认中国能向其他发展中国家提供经验教训，如今，他们不仅接受了这样的想法，还利用针对外国官员的培训项目来推销中国的发展模式。<br />
<br />
引言<br />
<br />
在1989年，紧随对北京天安门广场上支持民主的抗议者的残酷镇压，中国共产党（CCP）的道德与意识形态地位降到了有史以来的最低点。对统治阶层贪腐及特权的普遍指责广泛散布。关于社会主义的理想主义话语被看作是空洞的口号。天安门的屠杀显示出“人民的军队”会朝人民自己开枪。当时中国的农业经济已经部分自由化，而城市经济还紧箍在效率低下与腐败的单位制度铁框内。无论是国内还是国外，都没有人把中国看作是其他国家的模范。<br />
<br />
然而，在将近20年后，中共的威望却凭借长期的经济繁荣与复苏的汉族沙文主义(Han Chauvinism)这两股源泉得到了戏剧性的提升。关于中国逐步富裕会带来更多民主的预期（这曾是许多外国资本的美梦）在一党统治的延续下彻底破灭。迅速增长的财富大部分掌握在了成功增补了商界与知识分子圈子的政治经济精英集团手中；在中国远没有形成一个会挑战当局的中产阶级，这些群体反而更有理由在镇压人民群众中的“不稳定性”时占到统治者一边。这种镇压能否在当前经济萎缩的状况下继续还有待观察。与此同时，中共还谨慎地激起并塑造了中国的民族主义，现在许多国内的中国人都对中共威权体制下的发展模式感到骄傲。除审查制度之外，共产党的“思想工作”已经涉及到了对于教科书、电视纪录片、博物馆及其他传播其严重扭曲的中国历史版本等传播媒介的塑造。<br />
<br />
这一“中国模式”在国外已经开始广为传播。它对那些试图在促进经济增长的同时维持其权力的威权体制内的精英们来说有着必然的吸引力，甚至也已经开始争取到了不少发展中国家的普通民众，在那些国家里，几十年的自由市场改革始终无法带来广泛的经济增长。中国的统治者们将目标锁定在扩展其国际影响力并在世界范围内的资源争夺战中胜出上，同时寻找各种方法来将外国精英与公众吸引到这种“双赢”的安排中来。北京当局提供不附带任何人权要求的援助与投资，在中国为外国官员与学生开办培训项目，在外国大学里设立文化中心（孔子学院），并为其他压制性政权在联合国及其他场合提供外交保护伞。近几年来一个逐渐变得明显的现实是，北京当局与它在世界各地的威权主义盟友们都把中国体制视为民主制度的一个有力竞争者。他们的词典里保留了民主与人权这样的措辞，但在服务于威权体制利益的前提下对其进行了重新定义。甚至在一些民主国家或民主发展中的国家，包括泰国，中国模式的吸引力都开始不断增长。<br />
<br />
然而，尽管中国模式已经是民主价值的明确威胁，但它还不是势不可挡的。它的吸引力在很大程度上取决于未来中国经济将如何渡过这次全球衰退，以及它如何意识到在发展中国家可能碰到的磕绊。在国内战线上，中共比外界观察家们认识到的更加害怕本国的公民群体。“权利意识”近年来在中国民众中不断增长，这是一个无法与威权体制相容的现象。同样的，中共的国际交易策略也只排他性的涉及到外国精英阶层；最终的胜利还需要当地非政府组织、公民社会及媒体更多的支持。简而言之，我们要从“危机”这个词的原意来看待北京当局对民主所做的挑战――事件的进程会转向任何一方。<br />
<br />
国内的控制手段<br />
<br />
城市闪耀的天际线、巨额的外汇储备以及多数居民卡路里摄入量的增加所代表的中国的物质成就，显示出其政府的优先考虑是经济增长。而中国社会比起30年前不断增加的多样性，也显示出这个政权在寻求自由化。<br />
<br />
然而这些都是危险的误解。今日中共的首要考虑一如既往：保持绝对的政治权力。没有其他任何目标――比如经济的、军事的、外交的，或者民族主义的――能够胜过这个目的。当然，中共对最近的经济衰退的巨大关切准确地说也是因为其威胁到了共产党对权力的控制。<br />
<br />
【小资料：中国审查手段的商业化。作为其在威权资本主义体制下持续试验的一部分，中国共产党发展出了一套 21世纪的媒体模式。这一模式被证明同时具有韧性与镇压性。它包含了一种“基于市场”的审查模式，通过这一模式，当局能够同样对新旧媒体恢复控制。如果这些媒体偏离了党的路线，除了传统的政治与司法惩治手段外，当局如今还可以通过造成经济上的不良后果威胁到他们的出路。中国的编辑们与记者们在打破容许范围的限制时，会受到来自国家的降职、解雇甚至更严厉的处分。尽管现在中国的传媒业已经商业化了，比起政府补助来说更依赖于广告收入，但也必须考虑到惹恼与政府有密切联系的强大的商业利益所带来的财务风险。同样的，随着中国互联网行业逐渐显现出对国有媒体霸权的挑战，当局已经很快实施了基于市场的策略来查禁网络上会带来政治后果的新闻与信息。警察抓捕与判刑入狱的老办法被经常用来使网络活动家沉默，并且――如同在《网上自由》（Freedom on the Net），我们新编录的关于网络自由的索引中描述的那样――中国审查与控制在线内容的技术能力还无法与世界水平相比。但中国也已经在不断增长的将审查与监管“外包”给私人公司的趋势方面位于前列了。门户网站、博客运营商及其他企业都被要求使用内部人员来处理这些任务，如果他们不遵循政府的审查指示，就会有失去营业许可的风险。中国的这种现代威权体制下的媒体模式已经吸引了许多试图控制新闻与信息传播的国家和政府的注意。像柬埔寨与越南这样的国家都开始考虑这些手段，而中国在这方面已经是先行者。据信中国当局已经与该地区的政府共享了这种审查技术与专门知识。】<br />
<br />
在毛泽东统治时期，共产党制造普遍顺从的一项重要手段就是“思想工作”。这种意识形态化的强制力量的实施是公开的、露骨的，而且没有任何愧疚。如今，思想工作依然是中共维护权力的极其重要的手段，但它采取了一种更微妙的方式来运作。它是暗地里完成的，比如，通过机密电话打给报纸编辑，而不是大张旗鼓地。而且它是有目的性的：毛泽东时代的政治运动着眼于改造整个社会甚至人性，而如今的思想工作则集中在对中共统治生死攸关的政治问题上，其他方面则予以放行。但其效果依然足够深远。<br />
 <br />
按照通常的理解，审查制度主要是种种限制措施。一个政府或其他权力当局靠其来阻止违禁观点的表达。从这个标准来看，中共的思想工作当然属于审查制度，但这只是它所扮演的一半角色。另一半则是要积极扶持政府赞同的观点。思想工作独断的一面自建党伊始就是中共体制的一部分，如今它已经变得更加重要。它靠打压与拉拢手段的互相协同，对公众舆论施加着强有力的影响。<br />
<br />
打压<br />
<br />
中共很少依靠机械式或行政式的审查手段（比如封杀犯禁言论或迫使禁书下架），而更多依靠由恐惧造成的自我审查机制。在毛泽东时代及随后的一段时期内，自我审查都是通过发布一些广泛但含糊不清的禁令来实现的。一些诸如“批孔”或“反对资产阶级自由化”的指示会让人们不明就里，但却能足够清楚地表明违反将意味着付出沉重的代价。人们不得不通过自我检讨以及观察周围人的反应来猜测政府可能不喜欢什么。这种从众的心态使个人无法表达己见。任何胆敢在安全范围之外冒险的举动都会被视为“闯入禁区”。这样的人可能会受人敬佩，也可能被视为鲁莽。<br />
<br />
同样由恐惧造成的自我审查如今仍在继续，只是安全区与禁区的关系颠倒了过来。在毛泽东时代，表达只允许在特定范围内存在，而此外的一切都是禁止的。如今，人们则可以在某些特定敏感话题之外自由发挥，这些被禁的话题包括：1989年的天安门大屠杀、FLG活动、中国民主党、台湾独立、西藏与新疆的自治、大跃进时代的饥荒、高层领导人中的贪腐（这可以私下议论，但不允许在公开场合谈论），以及某些关于国内国际事务的“不正确”观点。这个清单现在还有可能包括对经济衰退下政府责任的看法。任何被认为是闯入禁区或作出触及政权切身利益的人，依旧是十分危险的。但是这些被禁止的领域范围是很小的，特别是与时尚、体育运动、娱乐、旅游、商业之类大而开放的领域相比，这就使得大多数人更容易回避开它们并乐于接受现状。如今恐惧已经不像毛泽东时代那样经常与明显，社会表面上看起来很正常。<br />
<br />
然而这种正常的表象掩盖下的是一个“柔性的”但无处不在的警察国家。它并不是一个统一的控制机构，而是一个由中央当局发布政策目标并由地方政府及其手下视情况实施的松散网络。在这种状况下，不同地区在压迫的程度与手段上都有着相当大的差别。再说如果进行了适当的自我审查，很多人都不会与警察国家发生冲突。而那些因反对某一领导人或做法而踏入禁区的个人首先会收到口头惩戒。如果这不起作用，他们还会受到便衣警察的骚扰，包括对电话和电子邮件的监视。接下来则是失业与登上黑名单，如果必要的话还会被关进劳改营或监狱，受到拷打甚至被处死。并没有很多人会沿着这个路径一路滑到底，但所有人都清楚走到底意味着什么。这就不仅能说明为什么自我审查机制会起效，而且还能解释为什么一个真正的公民社会不可能在中共治下形成。中国存在着难以计数的非政府组织（NGOs），但几乎没有例外地都受中共控制或服从其控制。而其他成员达到10或20人的组织都会被镇压。<br />
<br />
在这幅图景中最能被视为亮点的就是互联网，它在中共思想工作史上被证明是首个至少到目前为止无法被驯服的媒介。尽管对其已经进行了不少尝试。中共已经建立了一个监控互联网的警察部门，据估计已有超过30,000 人的规模。它利用从发达国家买来的技术，建立了能够关闭敏感话题评论、甚至抹除危险事项的过滤系统。同时也禁止在网络空间使用非实名并建立起连坐机制，如果在网页上出现不当评论，就可以据此关闭整个网站，而网站运营者也要背上责任。这个政权还建立了电子举报箱，任何公民都可以秘告其他人的不恰当言论。它还雇佣间谍人员，并利用黑客植入电脑病毒。尽管有这些压制手段，中国的大量网民们还是继续使用非实名；一些人用词语的同义替换来讨论禁止的话题；另一些用装作是虚构作品的方式来展现真实生活。这种猫鼠游戏如同互联网本身一样灵活善变并不断持续着。此时外国媒体的业务――特别是自由亚洲电台、VOA与BBC ――不仅对播放其传统节目，也对通过互联网提供不经审查的新闻来说都十分重要。<br />
<br />
拉拢<br />
<br />
中宣部（Department of Propaganda, 近期改为Department of Publicity）通常向记者和编辑发布秘密指令，用来规定哪些新闻和观点应该得到“强调”。1990年代早期，当邓小平试图就北京当局即将行使主权打消香港人的恐惧时，他做出了在“一国两制”框架内推行“港人治港”的承诺。后来，有意识到“港人治港”有可能开启更大范围的民主，官方向记者发布了低调使用这一口号的指令。“一个国家，两种制度”应该得到强调，特别要强调“一国”。<br />
<br />
近些年来，政府的许多宣传指令已用于煽动爱国主义，并使之与对中国共产党的认同联系在一起。教科书强调西方强加给中国的屈辱史，而新闻媒体则宣扬“西方压制中国”，西方有关人权问题的论调是别有用心的、是服务于这一目的的。民众从媒体得知，日本拒绝承认战争罪行，达赖喇嘛、台独和疆独分子试图“分裂祖国”。这些手法在年轻的城市精英中收效甚大，他们中的一部分人被称为“愤青”，这些“愤青”在互联网上发表的激烈的和沙文主义的言辞对于中国的未来是不祥的征兆。其他不这样激进的言论也清楚地表明了中国共产党思想工作的结果。<br />
<br />
这种指令的一个重要构成因素就是有选择地抹杀历史。毛泽东晚年的灾难――大跃进导致的饥荒、文化大革命――留下了一个对中国人价值观和公共道德有着重大影响的遗产（这种影响大多表现为“反弹”（recoil）的形式，如从极端禁欲主义和公开的理想主义到极端物质主义和公开的犬儒主义）。但今天在任何公共语境下径直讨论毛泽东时代仍是困难或不现实的。2008年北京奥运会的壮观开幕式有一部分节目回顾了中国历史，全世界的观众从古代王朝跨越到了1949年共产主义革命的胜利。但随后突然跳到了1970年代末期的“改革开放”。毛泽东时代的真实历史以及西藏、台湾、第二次世界大战乃至中国共产党本身往往都从教科书和其他媒体中蒸发，仅被姓名、日期和口号取而代之。当今中国的年轻人在数学、工程学以及外语方面可能受到了很好的教育，但对本国的过去了解甚少。更严重的是，他们不知道自己一直是怎样被欺骗的。<br />
<br />
思想工作是用奥威尔熟知的语言展开的。“对个人施加政治压力”被称为“帮助”，“侵权”被称为“保护权利”，政府通过名义上的工会控制工人，“压制维吾尔族”被称为“反恐”，“威权主义”被冠以“民主”，“真正的民主运动”被诬蔑为“反革命叛乱”，“依附性的法院体制”被称为“依法治国”。中国共产党思想工作的语言等同于弥天大谎。中国的政治权力取决于维持一种政治姿态，即使每个参与其中的人都知道该姿态是虚伪的。<br />
 <br />
结果<br />
<br />
过去几年，中国共产党的思想工作取得了很大成功。中国人表达民族自豪感的渴望强烈，而且这种渴望已被压制了两个世纪。经济的快速发展、中国国际地位的提高、奥运会金牌带来的荣耀以及其他用来释放爱国激情的阳光大道，中国共产党成功履行了创造这些光荣的承诺。例如，“已使数百万人民摆脱了贫困”。普通中国人知道实际上发生了什么，从1980年代开始至1990年代晚期，中共放松了经济事务的管制，但依然对其转化为政治事务的可能保持着警惕。由于最终在生活中的至少一个领域得到了自由，普通人激发出了为实现经济发展艰辛工作的巨大热潮，并带动了数百万人摆脱了贫困。同时，他们也把许多中共的领导人推到了金山上。但中国共产党在思想工作中，这个故事被解读为：党创造了一切、实现了一切、代表着一切。只要有可能国内的弊病就会归咎到外国人身上，比如目前中国的企业裁员就归罪到了美国银行的恶性。<br />
<br />
许多中国人仍为腐败、土地占用、剥削工人、贫富差距、苛扣养老金、苛捐杂税、水和空气污染、凶残的镇压而抱怨。缺少独立的监督机制和民主的纠偏机制的封闭政治体制是内在失调的，它无法实质性地处理这种抱怨。但中国共产党的思想工作采取了两种手段。第一，鼓动民众相信中央领导层是清白的，一切都是地方政府惹的祸。很多人都持这种乐观的观点。第二种手段是简单地转移注意力。对清洁空气的需求由52块奥运金牌做出了答复，拆迁户则为一项空间计划（译者注：神舟七号）目瞪口呆。<br />
<br />
为了分散注意力，中国共产党不时编造或扩大国家层面的恐惧感，许多中国人面对自身的时候，相对于遥远的台湾和西藏，更关心自己的日常生活。早晨起床后，他们最担心的是地方官员的腐败，而不是达赖喇嘛。但当中国共产党的宣传反复向他们灌输狼心狗肺的达赖喇嘛正在分裂祖国时，他们就会形成这样的看法：分裂祖国是卑鄙的，中国共产党才是阻止国家分裂的中流砥柱。煽动恐惧在以前并不存在，它与现实的政治风险无关，而与中国共产党强化公众形象和转移大众的注意力有关。近年来，中国共产党先后将日本、西藏、台湾和美国用于达到这一目的。在西藏的案例中，有迹象表明，暴力事件本身激发了这种恐惧。<br />
<br />
对于中国来讲很多方面都是有风险的，对世界也如此，这也是在一定程度上中国共产党思想工作打压、拉拢两种手段持续成功的原因。在一个对于本国历史的看法既狭隘又扭曲的未来人口中更多的所得将导致攻击性的沙文主义。1940年代的德国、日本以及1960年代的中国都存在这种可能性。我们仍希望这一模式不要生根发芽。过去的几年，公众的法律意识和人权意识有了较大的提高。所以出现了个体和集体形式的法律诉讼和抗议。中国共产党对这一趋势的过度敏感表明了它的潜力。一个萌芽状态的独立工会、教会和政治讨论团体引起了中国共产党的注意。如可能的话，还要对他们进行镇压和过滤。1989年大屠杀的20周年纪念被视为敏感，当局派遣便衣警察跟踪72岁的天安门母亲团体创始人丁子霖买菜。如果一群掌权者对一位72岁的老人感到恐惧，那么我们就可以认为它对自己的权力是感到不安的。<br />
<br />
国际影响<br />
<br />
在相对较短的一段时间内，中国与很多发展中国家建立了紧密的外交和经济关系。事实上，由于其迷人的攻势，中国留给很多发展中国家的印象要比其他大国积极，即使是在北美和欧洲讨论人权和贸易纠纷时的形象。这种迷人的攻势被看作是中国“软实力”的部分表现许多中国学者和领导人对“软实力”的看法超出了这一概念的创始人约瑟夫・奈(Joseph Nye)的观点。奈把软实力看作国家价值观的吸引力。但是中国共产党的定义涵盖了除外交和军事的一切领域，包括了奈认为是强制性的手段，如援助和投资。中国国家主席胡锦涛和党的其他官员已经清楚拥抱了软实力这一理念。这也成了他们论述中国在世界上发挥作用时所用的核心话语。尽管仅仅5年前中国官员和学者还坚决否认中国会为发展中国家提供经验，但现在，他们推翻了以前的态度，已经开始后利用针对外国官员的培训项目来推销中国的发展模式。<br />
<br />
【孔子学院：威权主义的软实力。中国用来扩大国际影响力、推销其治理模式的一个手段是迅速扩大的孔子学院网络。孔子学院通常由中国大学与所在国大学合办，提供中国语言、文化与传统技艺方面的教育。中国提供师资和教材。国外提供场地和设施。孔子学院由中国国家汉语国际推广领导小组（汉办）监管，该机构负责安排孔子学院的指导原则、预算与课程。汉办由12个国家部委的代表组成，包括教育部、外交部和文化部。孔子学院在早期将其宗旨描述为“通过提供中国语言与文化课程，促进世界上的跨文化交流，以提高世界对中国语言和文化的认识。”然而，一些观察家为孔子学院主办国的学术自由状况感到忧虑。孔子学院的一系列指导方案草案表明，对于台湾的国际地位以及与曾受到迫害的族群和宗教方面的少数民族的历史调查问题上，中国政府要求孔子学院按其指导方针的敏感性。“海外孔子学院必须坚持一个中国政策，维护中华人民共和国的独立和主权完整，远离所在国的任何政治、宗教和种族活动。”自2004年乌兹别克斯坦成立首家孔子学院以来，该学院的网络迅速扩大。现在78个国家已建立了 295所孔子学院。预计在2010年前总数将达到500家。现在，东南亚的孔子学院超过了70所，美国的孔子学院超过了40所，欧盟的孔子学院超过了80 所。其他孔子学院在南非、津巴布韦等非洲发展中国家成立。该项目雇佣了2000员工，有价值2600万美元的图书和音像资料。】<br />
<br />
在讨论软实力的过程中，中国共产党侧重于培训项目、有效的传统外交、孔子学院等新兴的公共外交手段以及对非洲、中亚和南亚极具吸引力的中国经济范例。然而长期来看，中国领导人需要扩大在发展中国家的民众中的吸引力。此外，他们也需要扩大或调整其软实力的能动性，以在欧洲等世界发达地区产生吸引力，与在美国相比，中国能在欧洲得到更有利的反馈。<br />
<br />
中共领导层追求软实力的理由是复杂的。一方面，中国在全球外交领域越发自信和老练。当今一代的官员清楚地认识到，北京当局必须积极地培育与亚洲、非洲及拉丁美洲发展中国家的关系。中国与世界不断加强的经济、政治与安全问题上的相互依赖以及对自然资源的需求已使它在国际事务中扮演越来越重要的角色。而从亚洲金融危机到伊拉克战争的一系列事件破坏了美国的国际形象，这为正在崛起的强权削弱美国及其盟国影响力提供了机会。另一方面，阿富汗战争和伊拉核战争彰显了美军压倒性的军事力量和技术，说明中国共产党选择硬实力的效用是有限的。<br />
<br />
最后，随着中国持续的经济增长没有受到新兴中产阶级的民主需求挑战，随着俄罗斯等创造了高增长率的威权国家，随着既有民主国家的经济过去五年间反复受到冲击，中国共产党的官员开始考虑其发展模式――而不是共产主义和自由事业之间策略性的折中――现实中可以在客观上成为优于自由主义的民主资本主义（liberal democratic capitalism）的合乎逻辑且可输出体制的可能性。为了表明并推广这一观念，中国共产党的领导人开始以自己的方式篡改并应用民主这一概念。就像普京领导的克里姆林宫将俄罗斯的威权主义称为“指导民主”（“guided democracy”）一样，中国共产党扭曲了关于民主的共识并抽走了民主的传统价值。此外，中国共产党的官员、智库和媒体不断指出，肯尼亚和吉尔吉斯斯坦的动乱已警告西方，自由主义的民主对多数发展中国家来说比并不合适。<br />
<br />
中国的软实力工具和战略<br />
<br />
在过去十年里，中国在全球的对外拓展（global outreach）都围绕在一个核心哲学上。不管在声明还是讲话里，中国领导人都阐明了一种双赢关系原则，鼓励拉美，非洲，亚洲和阿拉伯国家与中国，形成互惠互利的安排。双赢关系也关注不干涉原则，这对于开发中国家的领袖而言是尤其有价值的，这些领袖都目睹了殖民势力和冷战中的对抗双方在过去几十年中所做的干预动作。<br />
<br />
中共领导人扩展双赢理念到了一系列其他的领域上，声称要在全球贸易谈判中站在发展中国家一边，并把中国描绘成在联合国捍卫不干涉原则的国家。作为这一战略的一部分，双赢哲学是与西方的被北京当局描绘成推给发展中国家一个单一形式“民主议程”做法来做对比的。与此同时中共当局则积极提升其外交使团并利用高层交往这种传统外交手法来向发展中国家展示，中国是高度重视双边关系的，中国政府也已开始创立自己的区域性多边组织，如中亚的上海合作组织（SCO），它能用来对付促进民主的努力。许多外国领导人已接纳了中国努力争取国际领导权的作为。“他们国家是一个转型的榜样，”这是马达加斯加总统马克&#8226;拉瓦卢马纳纳（Marc Ravalomanana）2007年5月非洲开发银行在上海举行会晤期间对中方官员说的话。 “我们在非洲必须学习你们的成功之道。”<br />
<br />
中共似乎也体认到，有必要建立一个更广泛的公众诉求和促进民间交流的机制。这与过去的做法相比是个关键性的变化，旧做法的重点主要是加强与外国领导人的关系。北京当局已经建立起了中国青年志愿者协会（China Association of Youth Volunteers），一个类似于美国和平队（Peace Corps）的计划，旨在使青少年到埃塞俄比亚这样的国家去进行农业和语言项目。它还推出了孔子学院（Confucius Institute）项目，以支持在全球各大学里进行中文以及文化方面的学习。它越来越多地提供柬埔寨这样的发展中国家的小学进行中文教育的经费，在这些学校语言学习中获得成功的学生往往会获得到中国大学学习的奖学金。<br />
<br />
针对外国舆论领袖的培训计划也同样变成了显著的软实力工具。中国政府已开始为来自中亚、非洲，东南亚这些其他区域内的媒体工作者、执法人员组织培训计划。这些项目的部分是旨在向人们展示中国经济战略的成功之处，这种所谓的成功涉及到了部分的自由化，对某些产业的保护，以及保持某种程度的国家干预。<br />
<br />
发展援助可能是中国最重要的工具。在其他一些援助国表达了对人权的关注之后，中国已经证明它特别愿意加强对如柬埔寨、乌兹别克斯坦这类国家的援助。它还极大地提高了其对发展中国家的投资以及贸易，而那种投资往往是由优惠贷款所支持。在发言中，中共领导人表明，北京当局将是一个比既有民主国家更公平的贸易伙伴，将会帮助贫穷国家获得他们所需要的技术和技能，使他们自身能发展和富裕起来。同发达国家一样，中国也试图强调作为一个有影响力的贸易伙伴的作用，以争取其他优惠，在全球金融危机爆发之后，中国一直强调自己所拥有的庞大外汇储备，在掌控和打击经济下滑上将发挥积极作用。然而，这些通过削弱其他国家而获得的进展却由于工业化国家民众的情绪而复杂化了，那些国家的民众经常因为国内就业职位的损失而责怪中国。<br />
 <br />
中国外交伙伴的范围以及中国的对外拓展如何威胁到了民主<br />
<br />
中共的软实力工具对于中国各式各样的国际合作伙伴来讲都意味着不同的事情。区分与北京当局有外交关系的政府类型并检视这些关系如何危及到了民主是重要的事情。一方面，那样的国际合作伙伴中有着一群残酷政权的存在，包括了苏丹，缅甸，乌兹别克斯坦，北韩，和津巴布韦，其领导人正在联合国以及其他国际机构里寻求财政援助和保护。软实力的其他工具则与这些国家的政府无关，他们在向中国寻求经济改革方面进行学习兴趣不大。另一方面，在亚洲，拉丁美洲以及非洲这些发展中国家中在接受中国软实力的所有元素上又有着一个分化。他们正在寻求与中国开展经济，政治和文化上的联系，并且因为它们不是纯粹的威权国家，中国的吸引力可以扩大到公众层面。这种关系相比与一个独裁者或统治集团单纯的联盟关系要来得更有实质意义。<br />
<br />
当北京当局最初开始构建其软实力战略时，在程度上并没有如同普京统治下俄罗斯所制定的战略那样直接威胁到全球民主化，开始的时候这个软实力战略是旨在把邻国从推动对民主改革的道路上拉回来。然而，前苏联国家发生的“颜色革命”使中共产生了惊恐，而其他威权统治的大国的崛起让北京当局大胆的认为，也许存在着一个可转道的模式。此外，民族主义开始在中国国内构建起来了，并且整个民主推动运动面临到了全球性的强烈抵制。因此，中共的战略目标，开始更具攻击性地针对推动民主的作为。在过去十年里中国已更新了其访宾培训计划更为欢欣鼓舞的去兜售中国模式，而在很多方面去贬低自由主义的民主（liberal democracy）。如今，许多这类项目都专门集中在中国案例的研究上，主题则覆盖了经济制度建设，地方治理，或是创设一种司法体系上。<br />
<br />
培训项目往往涉及到这类讨论，即中共是如何设法开放其经济，使中产阶级站在政府这一边的，并避免类似俄国在转型期间以及其他许多发展中国家经济体所经历的社会政治混乱。特别是，中国已经开始为邻国的警察，法官以及其他负责安全问题的官员做大规模的培训项目。由于互联网过滤和管制一直是中共政权维系下去的重要组成部分，如何做成这些事情方法上的训练也都提供给了一些外国官员。中国政府已经提供了关于过滤和防火墙的信息和战略给缅甸，越南、沙特阿拉伯、乌兹别克斯坦和其他一些国家。<br />
<br />
这项努力的规模难以计算，但每年中国政府至少培训了1000名中亚国家的司法和警察官员，他们中的大多数都可以被归类为是替反民主的事业进行工作的人员。从长远来看，北京当局将加强其对非洲国家官员的培训计划，争取达到每年7000至10000名学员的规模。中国更广泛的援助计划的范围也同样是无法量化的，但根据世界银行估计，中国目前是非洲最大的债主国。在2007年在上海的一次年会活动上，中国领导人宣布，他们将为非洲提供200亿美元的新融资。<br />
<br />
在东南亚和中亚国家这个范围里中国的援助现在已经超过了民主捐助国所提供的援助。柬埔寨，是北京当局主要援助的受益国之一，北京当局与它之间的关系就提供了一个蛮有启发性的例子。中国政府是柬埔寨最大军事援助的供应国，其中大部分的援助都流到了被洪森首相（Prime Minister Hun Sen）当作政治武器来使用的反民主的安全部队手里。中国已向柬埔寨承诺了总额约6亿美金的援助。相比之下，美国目前只提供了5500万美元左右的年度援助给柬埔寨。缅甸的情况也显示了相似的趋势。中国政府现在是最大的援助供应国，而这些援助再一次又都用到了反民主活动上面。在过去5年以来北京当局已经提供了提供了两次2亿美元贷款给缅甸，这些“软性的”贷款通常是从不偿还的，基本上这样的贷款就相当于是提供给他们的赠款。而美国每年援助缅甸约1200万美元，主要是用于提供人道主义的援助和难民援助。<br />
<br />
这些培训和援助关系，使得北京当局及其合作伙伴各国的政府在他们各自的国内问题上可以去相互提供援助。例如，针对中亚国家的安全问题培训，已经给中共提供了一个机会去推动“维吾尔人是恐怖分子和分裂分子，并且他们威胁到了区域稳定”这样的认识。这种过程在过去十年中已经见到了成效，因为好几个中亚国家已开始遣返中国的维吾尔人，通常没有解释任何原因。与俄罗斯一样，北京当局也开始发展自己的非政府组织，其中的一些旨在模仿传统的民主推动团体。然而北京当局这类举动的目的不是要去建立民主制度，反而他们在政治和经济发展上建议东南亚和中亚国家成为致力于反对民主化势力中的一员。<br />
<br />
也许中国不断增长的全球影响力最危险的方面在于，其政府现在能够为缅甸，苏丹，乌兹别克斯坦和津巴布韦这类国家的威权统治者提供更广泛的外交保护和支持。由北京当局所创建的上海合作组织，作为制衡美国和欧洲在中亚影响力的国际性机构，在这一战略中扮演了关键性的角色。中国和俄罗斯都已经利用上海合作组织论坛，去批评推动民主的作为，当中亚的独裁者镇压国内要求改革和民主变动时对其进行支持。<br />
<br />
在联合国，北京当局已制衡了针对例如缅甸这种侵犯人权国家的国际压力，并且利用这样的时刻去改善了与（国际压力）涉及到的那些政权的双边关系。在2005年安及延市大屠杀（Andijon massacre）发生不久之后，美国和欧盟加大了对于乌兹别克斯坦的制裁，而中国政府则在北京招待了乌兹别克斯坦的领导层，并利用这个机会，提高了其获取乌兹别克斯坦自然资源的机会。当然这种模式倒也不是可以在任何情况下看到，在掌控 应对 -Drury Chiang 3/2/10 11:12 AM 顽固的北韩政权上中国已积极的与国家 国际 -Drury Chiang 3/2/10 11:12 AM 社会展开了合作。但这主要是因为北京当局将北韩的不稳定视为对中国的一种直接威胁，而对于北韩的政策议程当然不会包括促进人权方面的举措。<br />
<br />
对北京当局的挑战<br />
<br />
中国的软实力的攻势长期来讲是否会获得成功目前还是不清楚的。许多发展中国家都在担心与北京当局之间的贸易关系的特性，因为这种贸易关系往往着重在那些发展中国家的自然资源开采上，那将会阻止他们攀登价值增值的阶梯。这种情绪在诸如赞比亚的民粹主义政治人物迈克尔・萨塔（Michael Sata）身上得到了反映，他就利用反中国的情绪在2006年总统选举中寻求支持，虽然他竞选总统的努力最终失败了。事实就是大型的，有国有企业联系的中国能源和建筑公司习惯性的在海外工程中使用从中国招募来的中国工人，这使得他们并没有受到当地居民的爱戴。<br />
<br />
此外，当北京当局变得更为攻击性的推动其反民主的中国模式时，它也冒着成为它所批评的西方列强的镜像的风险，因为它也将“干涉”到别国的内政，而干涉的做法不是“西方列强”那种推动民主的做法而是去压制民主。虽然北京当局的不干涉誓言似乎受到了欢迎，发展中世界的一些领导人已开始迟疑，中国是否会信守这一原则。中国驻赞比亚的大使在2006年就警告说，如果选民选择的总统候选人是萨塔，那么北京当局可能与赞比亚切断外交关系。当与北京当局的蜜月期即将结束的时候，那些接受中国援助的国家的公民团体将开始直率的说出更多事情。许多活动人士逐渐认识到，中国的援助会导致环境破坏，糟糕的劳动标准，以及猖獗的腐败现象，并使得巩固的民主出现倒退。不过如果北京当局被证明是足够灵活的，在发展中世界的领导人层级以及公众层级利用其软实力的话，它还是可能对既有的价值观、理念以及民主的模式发起严重的挑战。<br />
<br />
我们的发现<br />
<br />
中共当局已经建立了多方面的和日益老练的一系列政策去削弱民主发展。这些政策是全面的，包括了政治，司法，社会和媒体等领域。<br />
<br />
中共已经故意引发以及形塑了中国民族主义，现在很多居民都对中共的威权发展模式感到骄傲。针对这个目的所做的共产党的“思想工作”，已经涉及到了对于教科书、电视纪录片、博物馆及其他传播其严重扭曲的中国历史版本等传播媒介的塑造。相关的努力是想要去引导公众的思维，民主这个词已被扭曲的面目全非，并从中剥离了它传统上所具有的那些价值。<br />
<br />
虽然对于媒体管制的生硬手段――骚扰，恐吓和监禁，仍然在使用，中共当局还是制定了更细致的方法去掌握媒体内容以及诱导媒体进行自我审查。这些措施包括了审查方式的商业化，当局透过这些商业化的审查方式去有效的外包对于互联网服务供应商以及其他私人网路使用者的审查任务。该政权还以一项雄心勃勃的，价值数十亿美元的计划，提升了其海外宣传的做法来增强对于国内媒体的控制。<br />
<br />
中国政府施加的国际影响力在以多种方式进行展现。当今世界上有着一群残酷政权，包括了苏丹，缅甸，乌兹别克斯坦，北韩，和津巴布韦――其领导人所谋求的是从中国那里获得在联合国和其他国际机构里的财政援助和保护。另一个更多样化的国家群则是在亚洲，拉丁美洲、非洲的发展中国家，它们接受了中国软实力的所有元素。他们正在寻求与中国进行经济，政治和文化的联系，但是因为它们不是纯粹的威权国家，中国的吸引力，是可以由政治领导人层级扩大到公众层级的。这种关系相比与一个独裁者或统治集团的单纯联盟关系还要来得更有实质意义。<br />
<br />
美国和其他民主国家必须更加对中共的软实力计划在世界各地的运作加以警惕，特别是他们如何保护和促进了威权统治这方面。民主国家必须确保前往中国，中国邻邦，发展中世界其他地区的外交官都更有能力了解这些软实力项目的目标和策略。在中国企业促进了威权主义的地方，民主国家的特使必须有对付他们的有效手段。这项工作不应只集中于中国以及中国项目上，也应该提醒东道国官员和公民社会民主的优点何在，威权主义发展模式的缺陷以及如果这种模式真正获得“成功”的话会带来怎样的危险。<br />
