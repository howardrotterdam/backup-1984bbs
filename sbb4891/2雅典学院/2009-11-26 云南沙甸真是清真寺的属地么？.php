<a href="http://club.china.com/data/thread/1011/2706/30/55/7_1.html" target="_blank">http://club.china.com/data/thread/1011/2706/30/55/7_1.html</a><br />
<br />
<br />
就事论事之前，大伙儿得搞清禁酒小组是什么东西先。根据沙甸穆斯林的宣传材料，禁酒小组是由沙甸大清真寺牵头领导，在全体穆斯林范围里倡导禁酒的宗教事务组织（见07年个旧市新闻），也就是说旨在穆斯林内部推广自个儿的宗教喜好，而且是个倡导小组，而非监督和强制组织。<br />
<br />
如果只是这么简单，必然不会成为新闻焦点。最近沙甸之所以在网络上沸沸扬扬，是因被人揭发出真相：1.禁酒原来一直适用进入穆斯林聚集村的非穆斯林租户；2.清真寺不但要求租客禁酒，还要求房东禁止租客在村里（包括屋内）食用猪油猪肉，房东宣讲不力会被处罚，而租客交流无效后会被驱赶。网站还附上了禁酒小组带领回族精壮围堵租屋、记录黑名单、驱逐住户，全村遍布圣训、诅咒等精彩照片。事后虽然有穆斯林出来解释他们驱逐之前都很有耐心，但这两个事实是没有改变的。<br />
<br />
这很直白地反映一个现实：清真寺绝不只是一个倡导教化的民间契约组织，而是已把沙甸当自己地盘，能按伊斯兰教义对世俗行为立法，要求全村禁酒、吃猪而不只是全村的穆斯林禁酒，如果违反可以动用族人暴力，而不用通过公权。我们还得注意一个重要事实：虽然个旧党委07年曾大赞穆斯林倡导禁酒，但三年来，禁酒扩大化、租客禁吃猪肉的事实从未进入政绩新闻视野。也就是说，地方政府一直在主动遮掩这个事实，他们清楚地知道这事清真寺在挂羊头卖狗肉，报道出来非但不是政绩，还很可能引发上级对政府公权虚设、有损政权稳定的追查。既然政府有此新闻纪律，我们又从哪里听到这边陲那些因饮酒被驱逐的汉人的怨言呢？<br />
<br />
