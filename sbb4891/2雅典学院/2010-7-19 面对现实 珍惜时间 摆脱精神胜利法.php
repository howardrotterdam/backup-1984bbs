这帖发完后，本人就将离开该论坛，一方面很多网友并不喜欢这样的帖子，另一方面，我深切的感受到，论坛年轻人还是多，脑子也比较简单，理想化，文学化，意识形态化的印记很深。我将在3、5年后再回来看看大家，你们所谓追求的民主宪政是前进了还是原地踏步了，有多少人能坚持到最后。我们再来盘算一下，中国的民主宪政需要几代人完成。<br />
<br />
我们追求民主，一个非常简单的问题必须问清楚，怎么追求民主，怎么实行，怎么操作，怎么去实现。如果你在这些问题前茫然不知所措，我劝你别继续浪费时间了，你每天花在网上的时间耽误了你不少学习技能的时间，这对你将来就业，家庭，孩子都没有任何好处。而且有些普通网友因为受到一些民运文章的影响，不可自拔，甚至被利用成马前卒，炮灰，变成人家的筹码，我就更奉劝你，回头是岸。一旦你劳教过，判了刑，你这一辈子就差不多了。有的人说我是在威胁，呵呵，我只讲事实，我只是非常善意的提醒你，我也没有能力威胁你，我和你一样，我也是坐稳了的奴隶。事实上，这些事情经常发生，只是你不知道而已。<br />
<br />
言归正传，第一个问题，你一定要回答，否则毫无目的的人，是做不了任何事的<br />
<font color="GREEN"><strong>中国要实现民主宪政，应该怎么做？</strong></font><br />
在回答这个问题前，我在第二个帖子中已经列举了甲乙丙的对话，可能对你有些参考，你可以理理思路。<br />
整天废话口号连篇，叽叽歪歪什么口水仗，那毫无意义。打字也很累，不瞒你说，我确实认识一些网评员，他们笑着跟我说，看到网上那些人每天打那么多字骂政府，幸苦的很，反而觉的挺可爱。我想，一个据说正在“强奸”你的人，还说你可爱，还是很悲哀的。更何况，在当前的网络环境下，他们随时随地可以删掉你的文章，实在很在鼓掌之间。你肯定要问，既然不怕文章，干嘛还要千方百计的控制？你问这个问题，太单纯，我回答你，不需要理由。权力主宰一切。<br />
<br />
第二个问题：<br />
<font color="Green"><strong>你担负的起损失吗？你的父母亲，你的女朋友会怎么看你？</strong></font><br />
天涯有很多曾经发贴的人，被请喝茶，从此便消失了，不再发那些刺激性内容了，现在的天涯差不多就是八卦了。不论是谁，和政府对着干，下场是可想而知的，天涯的有些作者在面对国宝的这类提问会比较后悔当初的冲动，一时兴起，图个爽，结果呢，留下不必要的记录。我看到在我的帖子里，有些网友年纪很轻，嘴巴很硬，口气很大，实际能力也是可想而知。我想，你不如先问问自己，行吗？担的起吗，别到了被狱霸弄了，才后悔当初。我不是威胁你，你也知道那些躲猫猫之类的事了。<br />
<br />
第三个问题：<br />
<font color="Green"><strong>你知道政治是怎么一回事吗？</strong></font><br />
这无关于正义，哲学以及所谓良知正义，政治就是较量。我想你有对政治的自己解读，应该也懂。这些问题，你回答完后，看看还有没有必要浪费时间去关注什么党啊，政府啊。深圳一位打工小伙，因为贫穷对社会不满，并结识了一些所谓民主人士，被吸收入号称中国民主党，结果呢？被判了10年。家里到处筹钱托人想尽一切办法，眼泪哭干，流落在深圳街头，而当初吸收他的那些民运的口号，和今天你在网络上看到的那些充满正义，良知，理论，充满社会责任感的文章一模一样，在东窗事发后，他们拍拍屁股无病呻吟两句，跑到一些海外小报上登个广告呼吁什么捐款，谴责、签名请愿之类。谁会理睬这个事情呢？他的母亲，眼睛哭瞎，直到现在依然在街头流浪，根本就没有人敢捐款。<br />
