													<h2>中国最危险的是老百姓的思维方式</h2>
						
						
																											<div id="postmessage_692371" class="t_msgfont">文章提交者：jianlin0405 <br />
　　我曾经碰到过这样一件事：好几年前的一天，我去邻居家玩，正好碰到老大爷看病回来，一进门就对医院破口大骂，说检查个骨质增生就用掉几百块，医生没有一个不是黑心烂肝的狗东西。过一阵子我和他儿子谈到了高考，老大爷听到了，斩钉截铁地说：一定要考医学院当医生，只要当上医生，工资一分不要都行，单拿红包和提成就几辈子吃不完。<br />
<br />
　　生活中，这种自相矛盾的思维方式的无处不在：我们对贪官恨之入骨，可同时我们又不顾一切的报考公务员，梦寐以求的要做官；我们对垄断国企骂不绝口，可同时我们又削减脑袋地想到那里求职就业，拿不合理的高工资；我们对办事情要找关系送东西深恶痛绝，可一旦自己要办事，我们首先想到的就是要找关系送东西；我们对特牌车愤愤不平，可是当我们偶然有机会坐在里面风光一把的时候，又觉得无比的痛快和神气……<br />
<br />
　　一句话，我们觉得愤怒，不是因为我们觉得不公平，而是因为我们觉得自己处在不公平中的不利位置，我们愤怒的目的不是为了消灭这种不公平，而是想方设法让自己处在不公平中的有利位置。我们许多人骨子里甚至是喜欢、迷恋、崇拜这种不公平的。<br />
<br />
　　这实际上也就是中国几千年来分分合合，低水平重复历史的根本原因。我们每一次农民起义，不是为了推翻压迫自己的制度，而是为了能成为这种制度的主宰，成为新的压迫者。<br />
<br />
　　为什么中国经历了几千年的不断跌倒，不断杀戮，老百姓的这种思维方式依然根深蒂固，从来不曾引起过广泛的反省呢？从哲学起源上思考，我惊奇的发现，不论是孔子还是孟子，他们哲学的最高目的就是“稳定压倒一切”。这两位圣人除了用神奇的汉字创造出一堆激情洋溢、朗朗上口的名言，最重要的贡献就是喋喋不休地告诫老百姓，你可以质疑人，但绝对不能质疑制度。汉朝的统治者发现了这其中的精髓，不遗余力地加以实践，使之成为万世不易的治国利器。今天我们不惜血本地向全世界推广这两位圣人的思想，不知道是不是依旧对这个利器恋恋不舍。<br />
<br />
　　尽管五四运动模模糊糊地意识到了老百姓思维里的这种腐败基因，但后来的统治者却是有意的在呵护和培养这种基因。我有幸经历了十六年的中国式教育，我发现教材里为了“普及法治”，确实长篇累牍地向我们灌输，要做一个遵纪守法的好公民，但教材里绝口不提政府才应该首先是法律的无条件遵守者，绝口不提公民有监督政府遵纪守法的权利，当政府违法的时候，公民可以通过什么途径对政府行为进行纠正。因此，教育教给我们的不是守法，而是顺从。要么是你顺从别人，要么是你让别人顺从。</div>

							
							
							
							
