<strong><font size="6"><font color="Black">美国世界日报社论----北京肯送大礼，台湾须懂收礼</font></font></strong><br />
<br />
<font size="3"><font color="Navy">http://www.cdnews.com.tw　2010-06-19 09:07:34</font></font><br />
<br />
　　在南非，世界杯足球赛刚好开幕，气氛炽热，在另一边厢的北京首都大酒店，气氛亦转热，因为两岸「经济合作架构协议」(ECFA)文本经第三次协商，达成共识，意味着大体尘埃落定，只等签署，诚如大陆代表唐炜形容，就如世界杯足球赛，已到了临门起脚的时刻。 究竟胜负如何分，是零和抑或双赢？ 作为弱势的台湾，在守住龙门的同时，又有没有起脚的机会？&nbsp;&nbsp;<br />
<br />
　　台湾面向大陆，本来就有小对大，寡对多，甚至弱对强的劣势。 大陆就凭着种种优势，可以利诱台湾，到最后吃掉台湾，因此台湾绝对不可卖身，只能不停转身，甚至变身，去挡住大陆，因此需要高度的警惕，与高深的技巧，甚至高超的触觉。&nbsp;&nbsp;<br />
<br />
　　触觉是什么？ 是对两岸「经济合作架构协议」的内涵，分歧、目标与出处，有全面的理解，才有全线的警惕，最后自能发挥全能的技巧。&nbsp;&nbsp;<br />
<br />
　　出处主要来自大陆占香港签署的(CEPA)，「更紧密经贸关系安排」，在香港经济最低迷的一刻，有大陆给予的支持与方便，使香港重新浮起来，但代价却是，大陆名正言顺地介入香港重大基建、金融经贸决策，亦同时削弱了香港人的政治抗争，一国逐渐凌驾于两制之上。 此北京的利诱，是另有目标的，因此台湾要警惕，不能步香港后尘。&nbsp;&nbsp;<br />
<br />
　　内涵的文本，包括序言、总则、贸易与投资、经济合作、早期收获及其他等五章，共16条组成，概括了两岸的主要经济活动，最重要的是早期收获，即提早降低关税及开放领域，项目清单多达500项，使台湾可获超过136亿美元的经济优惠，大陆对台湾开放项目包括石化、机械、纺织、汽车零配件等产业，其中石化项目约占台湾石化出口大陆总额六成，纺织约占八成，但大陆对台湾提出的早收清单，绝不包含农产品及台17项弱势产业，也不要求台湾开放大陆劳工赴台工作，因此协议是一面倒对台湾有利。&nbsp;&nbsp;<br />
<br />
　　为何会如此？ 是一面倒照抄大陆与东盟签署的「全面经济合作框架协议」，大陆为了要和东盟交好，如是便尊重东盟的主权，与东盟各国平起平坐，大陆为了要和台湾共存，于是和台湾的协议，便以东盟的协议为蓝本，期望台湾能初步委身，最后卖身！&nbsp;&nbsp;<br />
<br />
　　但协议始终会触及敏感部分，台湾要求协议签署后，两岸可共同参与亚太地区经济合作组织，当然包括与东盟的关系，否则台湾会被边缘化，大陆当然不愿因此而沾上国际色彩，大陆亦不满台湾身为世贸组织成员，对大陆的贸易限制，竟然多于其他国家。 不过，台湾应就此多让一步，以便争取更大的国际空间，大陆既以东盟模式和台湾制订协议，台湾何不就此走近东盟的领域。&nbsp;&nbsp;<br />
<br />
　　签署前夕，台湾还须顾及条文中，有关经济决策权、行政参与权的问题。 较早前，就协议应否尽快签署的问题上，马英九与民进党的蔡英文展开电视辩论，结果马英九大获好评，原因是蔡英文视这次的演出为辩论会，指出是与非，马英九却把它当作演唱会，唱出中听的、悦耳的，马英九对着台湾的群众，亦应当如此，甚至把演唱会变成流行曲演唱会，目前流行的又是什么？ 是群众利益的诉求，当然在争取利益的同时，不掉进大陆利诱的陷阱，会考验马英九的政治技巧。&nbsp;&nbsp;<br />
