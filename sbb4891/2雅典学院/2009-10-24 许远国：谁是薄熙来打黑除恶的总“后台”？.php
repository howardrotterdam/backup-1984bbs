<br />
前一阵子，网民们非常担心，薄熙来因文强这一硬，他肯定软了，真的有很强很硬的后台，他那个打黑除黑，也将中途夭折了，也就会很快鸣金收兵了。文强被捕，前后变化如此扑朔迷离，真的弄的不少人很尴尬的，也百思而不得其解的，人们猜测也是肯定的，说法也是很多很多的，版本也会各种各样的。比如，文强难道没后台？没后台谁提拔他？比如有人可能会想，文强案发生突变，是后台要倒了吗？后台也感到不妙了吗？后台要丢卒保帅了吗？所以都在猜测之中。接下来，又传出被捕的重庆黑社会成员在受审的时候集体翻供，这又觉得问题越发的严重了，薄熙来、王立军又会遇到麻烦了，现在可真的担心弄的不好就要鸣金收兵了！<br />
<br />
那么，说文强有后台，在不在理，不知道，但是，就本人看，薄熙来是有很硬的后台的，能做到今天的这一步，哪能没有后台，如果没有很硬的后台，他的打黑未半，就会中途崩殂的，如果没有后台，还能撑到现在？曾记否，前年七月份中纪委弄了个让官员在一个月内限期交待不明财产，最后就不见音讯，是不是历历在目，记忆犹新呀？现在薄熙来搞这么大的动作，怎么能没有后台？那么谁是他的后台？当然，王立军是有后台的，他的后台就是薄熙来么，因为是薄熙来把他从辽宁调过去的，不用说的。因为一个蓠芭有二十四个桩，一个好汉也得二十四个帮，所以，关于谁是薄熙来的后台？不说网上议论纷纷，在最普通的老百姓的坊间的茶余饭后也是不断的议论的话题。<br />
<br />
我觉得，薄熙来的后台有，而且很好很强大，一个是我们的党，是他的后台，没有这个党组织的支持，薄熙来就是以个人的名义，再想打黑也是没有办法的，因为他调动不了武警，调动不了警察，党不忍许黑社会现象存在，国家法律不忍许黑社会横行，所以有了这点，就是他的总后台，凭着这个后台，就可以调兵谴将，就可以向黑社会势力开战。第二，重庆人民是他的后台。如果重庆人民不是倍受黑社会压榨之苦，薄熙来提出打黑，也不会得到那么多人民的支持，没有群众的举报，谁知道黑社会在哪里？谁是黑社会？如果群众不相信薄熙来，说他是虚晃一枪，不动真的，谁向他举报？所以，打黑得到支持，当地的群众是最可靠的后台。第三，全国人民是薄熙来的后台，薄熙来打黑除恶，一直不是孤立的，不是他一个人在战斗，而是全国人民都在积极声援，而且是在不停的声援，不断的声援，有了这样的精神动力，薄熙来一直撑到现在。薄熙来师出有名，当然就有更多的人民成为他的坚强后盾，所以薄熙来的后台是很硬很硬的。<br />
