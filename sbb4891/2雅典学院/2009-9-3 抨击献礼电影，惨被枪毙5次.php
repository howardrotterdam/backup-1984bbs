													<h2>抨击献礼电影，惨被枪毙5次</h2>
						
						
																											<div id="postmessage_224654" class="t_msgfont">平媒约稿，毙掉了。后面我又给了4家媒体，也陆续传来“不可能用”的噩耗。<br />
<br />
献礼电影献给谁？<br />
<br />
宋石男<br />
<br />
2009年9月，包括《建国大业》、《天安门》等在内的30部“高质量的献礼影片”陆续登场。献礼电影乃是中国特色，而且是颇为自得的中国特色。欧美及其它亚洲国家，罕有此习气。<br />
<br />
1976年，美国正当国庆200周年，并没有拍摄诸如《少年华盛顿》、《林肯大总统》、《来克星敦的枪声》、《西部大开发》一类的献礼电影。有网友钩沉当年20部最卖座电影，除了《洛奇》勉强算主旋律的励志片外，其余多是不和谐之音。譬如《总统班底》，瞄准水门事件，猛揭政府阴暗面；再如《出租车司机》，讲边缘青年暴力事件，也算是反映社会阴暗面。<br />
<br />
1989年，法国正当国庆200周年，拍摄了史诗片《法国大革命》，不过这部电影跟献礼片有相当大的区别，全片对大革命更多的是反思与批判，而非无情的赞美，也不可能为当下政权的合法性或光洁度保驾护航。<br />
<br />
2006年，意大利正当国庆60周年，我吃力地浏览了当年200余部意大利电影的目录及介绍，没有一部与献礼有关。<br />
<br />
就连热血的韩国人，在2008年正当国庆60周年时，逾百部电影也无一献礼片。由于市场不景气，这年韩国电影只有7部盈利。如果拍摄献礼片，市场或将更不景气。<br />
<br />
上述这些欧亚国家，其实也有美国梦，韩国血一类的电影，但他们的作法是市场主导，而非行政暗令。这类电影以市场化形式包装出来，脸没绷那么紧，也无涉最核心的意识形态。更重要的是，这类电影的市场不是官方人为制造的，也不是投资垄断、发行垄断之后硬性制造的，而是部分观众的大国民心态自然酿造出来的。<br />
<br />
回到2009年的中国，我们一下子搞出30部“高质量的献礼片”，究竟是要献给谁呢？即使最托大的发行商，恐怕也很难夸下“市场将热捧献礼片”的海口（《建国大业》或是例外，毕竟它有100多位海内外华人明星加盟的豪华阵容，对特别喜欢熟面孔的中国观众们，是个不小的诱惑。至于国家电影局长童刚预测“30部献礼片票房将达8-10亿”，我们应当原谅他的乐观。当然，在利用各种行政手段“组织看，逼着看”之后，献礼片票房大捷也是可能的，那就请童局长原谅我们的悲观）。在推特，我随机调查了过百位电影爱好者，坦言“献礼影片，我看好你哟”，“献礼影片，我好期待好期待呀”的人，不到十分之一。如果献礼片不是献给市场，也不是献给百姓，那么只好说是献给共和国了。然而共和国是多么模糊不清的一个概念，当我们使用它的时候，往往只是在说其背后的执政者。<br />
<br />
当然，献礼影片也有正面效益，它可以增加民族凝聚力，渲染国庆喜庆氛围，为共和国六十年的辉煌历程树碑立传。然而，它毕竟带着浓厚的“文艺为政治服务”的烙印（往狠里说甚至是“文艺为洗脑服务”），难免让人感觉不适。<br />
<br />
上世纪40年代初到70年代末，“文艺为政治服务”曾是党的文艺政策的灵魂。1942年5月毛泽东《在延安文艺座谈会上的讲话》发表，正式提出“文艺为工农兵服务”、“文艺从属于政治”、“文艺服从于政治”。在此种政策指导下，只能收获八大革命样板戏电影。直到1979年11月第四次文代会前后，新的领导层才提出“文艺为人民服务”、“文艺为社会主义服务”这一相对宽泛的“二为”方针，以取代此前更狭隘的口号。而1980年以后的电影，也渐有起色，回归民众，回归市场。<br />
<br />
时至今日，“文艺为什么服务”在我看来已成伪命题――文艺可以产业化，也可以小众化，但它只存在受众问题，不存在服务问题。若认可此观点，那么“献礼电影献给谁”，自然也成了伪命题。套句流行语来说，献礼电影献的不是礼，是寂寞。</div>

							
							
							
							
