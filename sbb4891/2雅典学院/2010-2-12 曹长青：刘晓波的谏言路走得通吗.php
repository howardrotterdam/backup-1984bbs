													<h2>曹长青：刘晓波的谏言路走得通吗？</h2>
						
						
																											<div id="postmessage_477904" class="t_msgfont">――曹长青：刘晓波的谏言路走得通吗？<br />
<br />
&nbsp; &nbsp;来源:观察 | 日期:2010-02-12 | 网址: <a href="http://www.aboluowang.com" target="_blank">http://www.aboluowang.com</a><br />
<br />
&nbsp; &nbsp;这篇文章本来在《零八宪章》刚推出的时候就准备写，结果因刘晓波被抓走而没写下去。现在刘晓波被判11年，愤怒之后慎思，觉得还是应该写，算是异议人士中的异议声音吧。无论在哪一个群体，在多么“崇高”的事业中，没有异议都是不正常的，甚至是可怕的，都可能会导致更多的刘晓波被抓、被判。<br />
<br />
&nbsp; &nbsp;《零八宪章》的调子是温和地呼吁中共改革，这已是共识，不需要我再强调。宪章除了“前言”的套话和当然没错的“基本理念”，其主要内容是“我们的基本主张”。而这个基本主张的关键是第一条：修改宪法。<br />
<br />
&nbsp; &nbsp;中共宪法不能修，只能抛弃<br />
<br />
&nbsp; &nbsp;我认为这种“修宪”的“谏言”之路是走不通的，因为：<br />
<br />
&nbsp; &nbsp;第一，中国目前的宪法，整个是一部“国家”泰山压顶般高于“个人”的宪法；一部以保护政府为主导精神的宪法（美国宪法则完全反过来，其全部精神是：保护个人权利，限制政府权力）。对中国这种宪法，压根不存在“修”的问题，只有“抛弃”这唯一的出路。<br />
<br />
&nbsp; &nbsp;第二，即使对这样一部宪法，中共都根本不执行，建政六十年从来都没有执行过（还需要比判刘晓波本人的案子更清楚的证明吗？），那么修宪的意义在哪里？<br />
<br />
&nbsp; &nbsp;第三，在一党独裁的现状下，修宪机制怎么产生？人民没有选举权，没有选举产生的国会。那怎么修宪？谁来修宪？<br />
<br />
&nbsp; &nbsp;第四，在一党独裁的现状下，只有共产党修宪这一条路。但在共产党的完全主导下，能有修出一部民主宪法的可能性吗？<br />
<br />
&nbsp; &nbsp;第五，即使我们不谈中国宪法是一部“国家高于个人”主旋律的宪法，从理论上来说，是不可修，只可弃的，也不谈中共一党统治下不可能修；仅从技术层面，不知道有多少朋友看过中国的宪法（现在网上一查就可以找到），那是一个逻辑到处漏洞，前后条款矛盾到惊天动地的东西，怎么还有修的可能？怎么把那些逻辑混乱理清？怎么把那些矛盾冲突摆平？<br />
<br />
&nbsp; &nbsp;事实是：共产党不垮台，修宪就只是共产党一家的事儿；而只要共产党修，怎么修，都是以保住共产党的独裁为中心思想。所以，在“修宪”从内容到形式上都完全不可行的前提下，再谈什么三权分立、独立司法、宗教自由、甚至环境问题等等《零八宪章》后面那些“十分正确”的条款，就不仅毫无意义，甚至是滑稽了。<br />
<br />
&nbsp; &nbsp;别期待“狼”改邪成“羊”<br />
<br />
&nbsp; &nbsp;那么在这种情况下，这《零八宪章》有一万人签名，一百万人签名，又有多少实际意义呢？当然，没有实际操作意义也没有关系，如果它能传播真理，把揭示专制本质的声音传到千百万人的心中，那这个《宪章》的价值也是重大的。但问题是，《零八宪章》这种“谏言”的形式和内容，所起到的瓦解共产党政府的作用大，还是潜移默化强化人们承认这个党统治的合法性的效果大，实在值得探讨。我本人认为是后者。因为共产政权本身绝不可能改变其独裁本性，这是历史已经证明的事实。所以，如果一再呼吁它改邪归正，就无形中传播了“它有改的可能”这种信息，因此而增加人们对“党”和那个政权的期待；而这种期待就是延续那个独裁政权的最重要根基。只有人民从骨子里确信，共产政权绝没有改变性质的可能，推倒它的契机才可能产生。<br />
<br />
&nbsp; &nbsp;而且“谏言”这种思维方式和做法，别说中国封建统治几千年，共产党专制六十年，六四的鲜血淋淋还没有把连下跪谏言都遭砍头的事实说明白吗？难道还需要刘晓波再做11牢才明白共产党的本性吗？这种调子持续下去，即使再有一次“八九民运”，也依旧会是在热血澎湃的爱国主旋律下的要求党内改革的继续，更何况，再一次大规模民主运动的影子都看不见呢。<br />
<br />
&nbsp; &nbsp;有人会说，那你躲在美国让我们喊打倒共产党去送死呵。首先第一个层面的回答：追求自由是自己个人的事，只有其结果是公众可以分享的。没有任何人有权要求谁，更没有人要求谁去牺牲；谁都可以选择沉默，或选择做奴隶。别人只是评论那些自己选择追求自由的人，你这样做是否离自由更远。每个人扮演什么角色，也都是他自己的选择，别人也只是评论，这个角色所起的作用是否会适得其反。<br />
<br />
&nbsp; &nbsp;第二个层面的回答：刘晓波站在中共的对立面，说过很多到位的话，并没有被抓。但那些想和共产党套近乎的，都给他带来牢狱之灾：《六二宣言》高呼“我们没有敌人”，结果被抓进去关了一年多，如果没有悔过书，一定会被判几年；《双十宣言》呼吁国共两党要尽快联手促进和平统一（那个宣言要民主政府和独裁政权联手组成“联合当局”，实荒谬不堪！），被控要“颠覆政府”，判三年劳教；《零八宪章》呼吁修宪，肯定共产党“开始了从拒绝人权到逐渐承认人权的转变”，于是再被控“颠覆政府”判11年。这就是去跟中共政权讲普世价值、人权、修宪，期待“狼”改邪成“羊”的结果。<br />
<br />
&nbsp; &nbsp;错在相信“请求”就不会死<br />
<br />
&nbsp; &nbsp;二十多年来，刘晓波写过很多揭示专制本性，远比《零八宪章》深刻、真实的话。如果他这次是因为清晰地站在独裁者的对立面，或者只是为他自己署名的东西而坐牢，相信在很多人心中会是远比现在更高大的英雄。但为一个降格、折扣的东西，作如此重大的牺牲，这不是值不值的问题，而是对不对的问题。<br />
<br />
&nbsp; &nbsp;但既然要求修宪也是“颠覆政权”，不如真犯“罪”。如果站着、跪着都是死的话，为什么不站着死呢？其实问题就在这里：是因为相信跪下“求”就不会死。但是，如果相信跪下求就不会死，那就是对专制的残酷本质没有足够的认知，这又和刘晓波那些深刻的批判专制的文章有巨大的矛盾。那刘晓波到底是怎么回事？<br />
<br />
&nbsp; &nbsp;他或许会回答：“我不降调，没几个人跟我签；而只有签名的人多，力量才大。”那么这里值得讨论的就是：到底是人多签一个降调的、甚至传播错误信息的宣言起到的作用大，还是个人发出真实的声音起的作用更大？且不说，即使这个相当降调的宣言，也不可能在官方报刊上发出来，也只是在海外网络发表而已，能看到的也只是关心这个问题的小圈子而已。就这样一个联署信，在国内任何刊物上都发表不出来（不实践一丝一毫中共宪法的言论自由）的现状下，还要求它去修宪，岂不荒诞？<br />
<br />
&nbsp; &nbsp;提出“修宪”要求，就是相信中共有政治改革的可能。而吉拉斯在1957年的《新阶级》中就否定了共产党有改革的可能性；他在戈尔巴乔夫的改革受到世界瞩目时说：“戈尔巴乔夫想改革共产党，这是完全不可能的。共产主义是无法改革的。”半个世纪之后（半个世纪！），在苏联、东欧彻底结束共产统治二十多年之后，在六四屠杀发生二十多年之后，中国的先锋自由派知识分子还在期待“修宪”，这难道不太令人悲哀了吗？<br />
<br />
&nbsp; &nbsp;喊出“皇帝光着腚”才重要<br />
<br />
&nbsp; &nbsp;如果要想以人多显示公民力量，应该是以“抗议”为主，而不是“建议”。两者性质不一样，角色不一样。如果做不到一群人发出更强的声音（谴责人权在中国如何被践踏），那么个体的真实声音，也比妥协之后的群体虚话（甚至错话），更有意义。需要的是一句真话的种子，撒在无数人的心中，给人们提供另一个看问题的视角。面对一个裸体的皇帝，到底是一个人说一句“皇帝光着腚呢！”更重要，还是一群人说：“哎呀，皇帝的新龙袍太单薄了点，会着凉的，再加件布衫吧”更重要？如果一个人不敢说皇帝光屁股，一群人商量一齐说，那还有意义。一个人有胆说，反而要找一群人降调，我理解不了这个逻辑。<br />
<br />
&nbsp; &nbsp;你现在不是从政（连选举都没有，哪有从政的资本？），所以中国异议势力不存在搞联合阵线、联合政府思维的东西，而是应把真话说到位。大概多数签名者都没有从政之心，也未见得都赞同《宪章》的主张（有人说连看都没看过就签了），或许都只是抱着同样的心态，人多就声势大一些，而这种声势似乎又可以增加其正确性。但历史什么时候证明过，越多人认同的东西就越正确？就一定能成功、胜利？<br />
<br />
&nbsp; &nbsp;恰恰相反，人类历史绝不是靠多数推进的，而是靠极少数人的正确的理念。他们一开始虽然从来都是极少数，但就因为正确，所以终究能够赢得最后的胜利。今天，绝大多数中国人是认同共产党继续统治的，难道他们人多就更正确？大概所有《零八宪章》的签名者都不这样认为，因为他们本身已经是十三亿中的极少数的公开反专制者。<br />
<br />
&nbsp; &nbsp;举西方最近的例子来说，一百多年来，在全世界的知识分子中，反对资本主义的声音从来都是绝对的多数。我来到美国才近乎震惊地发现，为资本主义呐喊的知识分子少到令人目瞪口呆。更具讽刺意味的是，推崇资本主义的经济学家们都饥寒交迫，找不到有很好经济保障的教职或学术职位，而为社会主义（甚至共产主义）呐喊的则个个待遇丰厚，桃李满天下，享尽资本主义的好处。直到共产主义已经彻底失败的今天，全球绝大多数知识分子还都在一直拼死抗衡资本主义。但不管反对的势力多么强大，资本主义在顽强地往前推进着；尽管每一步都艰难万分，都迎着镰刀斧头的砍杀，但资本主义是走在胜利的道路上，就因为它正确，而不在于支持它的知识分子的人数。<br />
<br />
&nbsp; &nbsp;我举这个例子，是想说明，多数未见得代表正确，反而经常在错误的方向。所以，总想靠人数来获取“正确”的信誉和力度，是没有意义的。而一个正确的原则，哪怕开始只有一个人支撑起来，也一定会赢得最后的胜利。<br />
<br />
&nbsp; &nbsp;不能同时坐两把椅子<br />
<br />
&nbsp; &nbsp;肯定会有人异议：如今老百姓、知识分子都只顾赚钱，话说给谁听呢？没人响应，说了不是白说？其实这没什么值得沮丧和悲观的，多数人从来都是只顾赚钱吃饭的，这是正常的；你只说给那少数的思想共鸣者听就足够了。真正有机会的时候，不是多数胜利，而是正确胜利。<br />
<br />
&nbsp; &nbsp;萨哈罗夫提出“一个人不能同时坐在两把椅子上”，你要么附和共产谎言，要么起来反抗，传播真实。而后者是个极高的标准，鲜少有人能去做。所以，许多反对专制、追求自由的人可能这样认为：我当然不想附和共产谎言，但在中国目前状态下，不用这种软性的方式，还有什么路可走？换句话说：如果革命硬拼不可能，婉转说软话，祈求在体制内改革也不对（事实证明也走不通），那你说中国的事怎么办？中共政府到底怎么才能垮台？<br />
<br />
&nbsp; &nbsp;我不知道它怎么垮台――目前像中国这么大国家，如此经济腾飞、政治独裁的情形，人类史无前例；面对一个前所未有的局面，任何推测、设想都没有足够的依据，所以没有意义。我只坚信它一定会垮台。或许因发生大的经济动荡，导致某个环节的契机；或者党内斗争导致转机；甚至也不排除有魄力的军官发动政变，百姓处于对中共的积怨而呼应支持（罗马尼亚模式能叫暴力革命吗？他们武装结束了共产政权，同样建立了民主制度）。我知道的是，应该一句、一句传播到位的真话，一颗一颗地播种追求自由的种子；那么一旦契机到来，那些潜移默化到人民心中的思想，就会产生巨大的力量，促成那个政权的垮台。<br />
 而那些有意无意强化人们对共产政权持续期待的信息，只能导致在有机会的时刻，又促使相当一批人（包括可能起到重大作用的中共高层领导人）产生对现政权的幻想，导致可能的胜利再次流产。共产党抓判刘晓波本身（还有多少其他人被捕、被判、被监控！），证明了《零八宪章》所说的中国政府“开始了从拒绝人权到逐渐承认人权的转变”的错误。这种思维持续下去，不仅抓不住契机，更不能创造契机。<br />
<br />
&nbsp; &nbsp;六四的血洗亮眼睛了吗？<br />
<br />
&nbsp; &nbsp;人类古往今来，多少事情都是“意外发生”；越近代，越意外；有些所谓被预言得的很“准”的事情，只不过是由于人类从不接受教训，于是太过于频繁地重复历史，所以人们以为智慧的祖先预言得很准。事实是，你只有摔过跟爷爷一样的跟头，才相信爷爷的话是对的――我们的爷爷，我们的孙子也不例外。所以，中共的垮台也很可能又是一个让全世界跌破眼镜的“意外”，但这个意外必定有它内在的必然；而正确的思想，才是推动这个必然的力量。<br />
<br />
&nbsp; &nbsp;我在好多篇文章中谈过，知识分子应该清晰，你到底是跟那个政权说话，还是跟人民说话。不跟政权说话，不等于具体的共产党人和中国政府官员听不到你的声音，更不等于不会促使共产党内部、高层瓦解，最终从体制内崩溃。在美国独立革命的时候，连华盛顿都是从潘恩的《常识》得到启发和鼓励，最终打赢那场战争。<br />
<br />
&nbsp; &nbsp;如果《零八宪章》是刘宾雁起草的，我当然同样不会赞同，但却完全理解；他至死相信马克思主义，只认识到要对党第二种忠诚的程度。而刘晓波是认识到了共产专制的本性（但他的再次“我没有敌人”又令人瞠目结舌），所以，他让我不可思议。<br />
<br />
&nbsp; &nbsp;刘晓波令人遗憾之处，是从正确的个人独立思考，向错误的群体意识妥协――从黑马走向黑点，硬要往人群里挤。从站在专制的对立面，迈向一再给朝廷递状子谏言（修宪要求决定了这个《宪章》是跟谁说话，老百姓不能修宪）。而恰恰是递状子谏言，使他撞上了中国历代皇朝的一贯做法：重罚谏者――因为在皇权看来，“谏”则是图谋政权。<br />
<br />
&nbsp; &nbsp;刘晓波被判11年，他自己可以高姿态地表示“没有敌人”、没有愤怒。但我对这个典型的因言治罪的政治案、对最近一系列嚣张地残酷镇压异议人士的暴行极为愤怒，对那个邪恶政权（自由公敌！）所犯下的罪孽永远铭记，绝不宽恕！<br />
<br />
&nbsp; &nbsp;迄今为止，《零八宪章》所起的最大作用，是由于刘晓波的被捕、被重判，让一些人“恍然大悟”：原来共产党没法治。肃反、反右、文革、六十年的暴政还没让人们明白共产党没有法治吗？六四的血居然没有洗亮刘晓波的眼睛吗？面对一个如此邪恶的政权，中国文化人到底还要经过几个六四、多少个刘晓波被判之后，才知道共产党是不可改革的呢？！<br />
<br />
&nbsp; &nbsp;2010年2月8日于美国</div>

							
							
							
							
