薄熙来在什么情况下会发动军事政变？ <br />
――邓朴方下重庆 薄熙来与其冰释前嫌？ <br />
<br />
<br />
作者：姜维平&nbsp; &nbsp; <br />
<br />
　 <br />
<br />
邓朴方一行在重庆 <br />
<br />
<br />
中共18大越是临近，薄熙来越是焦虑，虽然，胡锦涛最近放话“干部任职可以不受年龄限制”，但年近退休线的薄熙来依然忐忑不安，为了集聚太子党的力量，加强他接班的合法性，他使出浑身解数，充分表演，以“为穷人造房”的响亮口号迷惑百姓，还一改常态，放低了身段，邀请邓朴方访问重庆，我认为，这是一个微妙的中国政局的新动向，它似乎表明薄熙来用文强案顶住了团派对其贪腐的调查，已自保平安，很可能反败为胜。 <br />
<br />
<br />
据重庆媒体报道，6月13日，全国政协副主席邓朴方一行在力帆汽车制造厂考察电动车项目。《重庆晚报》还配发了照片。这是6月11日至14日期间活动的一项，报道说，邓朴方在渝考察重庆市残疾人工作情况时指出，要认真贯彻落实中央7号文件精神，坚持把残疾人工作与党和国家工作大局紧密联系，扎实推进残疾人社会保障体系和服务体系建设，为残疾人群众创造更加美好的生活。 表面上看，这是邓朴方的份内之事，但这些“大话”，“套话”，“空话”不过是烟幕弹，了解薄熙来与邓家关系的人可以看出两个问题：第一，以前他们关系不睦是公认的事实，这回是否要冰释前嫌？第二，2007年12月薄熙来下重庆，绝非其本意，他卧薪尝胆之始，为何邓朴方没有明示同情，而今却忽然与叶挺的后人一样，去给薄熙来捧场？ <br />
<br />
<br />
我认为，在中共太子党里，邓朴方是卓有影响力的人物，多年来他一直是薄熙来的一块“心病”，在大连任职期间，薄熙来与许多高干子弟来往密切，互通有无，但有两个人几乎老死不相往来，一个人是习近平，一个是邓朴方，在我印象中，薄熙来对邓朴方下属的康华企业在大连设的分公司也保持距离，从未听说他和邓家的人私下交往，或生意上给他们任何好处，现在情况似乎发生了巨变，薄熙来抓住时机，向邓朴方伸出了橄榄枝，他要和他冰释前嫌！因为只有这样，他才能攻破太子党群体里最后一个不认同他的坚强堡垒，如果邓家的人也由过去的拥护胡锦涛，转向依靠薄熙来，那麽，中国政局可能会有大变，只要冲进政治局常委，薄熙来将转败为胜，胡温必将成为可怜的阶下囚，像邓朴方这样的既得利益者，经历了文革灾难，他可不想重蹈覆辙，所以，薄熙来的 “唱红”，首先温暖了他的心，给他吃了一枚“定心丸”。薄熙来的“打黑”，使他似乎看到了其父邓小平对民主运动坚决镇压的果敢身影和血腥手腕，他觉得薄熙来比习近平更令中共太子党放心，尽管他们以前有矛盾，有隔阂，但毕竟大家都是一条船上的生死与共的“政治联盟”，只要不被人民推翻和清算，不失去奢侈豪华的物质利益，他们愿意求大同存小异，冰释前嫌。 <br />
<br />
<br />
而且非常重要的是，胡锦涛为了延缓习近平依次接班的速度，有意让薄熙来以“党内民主”为幌子，大肆表演，薄熙来抓住老百姓的心理，咬住“团派”提拔的文强，揭开了中共地方官员与黑社会一体化的黑暗内幕，一方面和胡锦涛，汪洋，贺国强进行了肮脏的交易，改变了自身贪腐被调查的危险处境，使王珉在辽宁举棋不定；另一方面，又通过媒体，为自身涂粉抹脂，欺骗舆论，争取了民意的支持，给胡温施加了巨大的民意压力，可谓一举两得，以退为进。面对这一切，可能太子党内部发生了最后的力量对比的变化，由过去的主要是叶剑英家族后人力挺薄熙来，变成了如今邓家后人也认同他，顺势加入的新局势，如果我的分析没错，这标志着薄熙来已成为中共太子党集团的总领军人物，他已告别了2009年5月20日“将军后代合唱团”的彩排，正式地与胡温为代表的团派展开了生死大决战。不论如何，依我对薄熙来的深入骨髓之了解，只要有可能，他将在太子党的支持下，果断地发动军事政变，如力量不足，他会继续运筹，于18上志在必得，一旦权力在握，他绝对不会放过任何一个曾挡住了他仕途的人，中国将囹圄成城，血流成河！ <br />
<br />
<br />
据重庆媒体报道，邓朴方考察了重庆市容市貌，听取相关情况汇报后说，近年来重庆克服重重困难，经济社会取得了飞跃发展，各个方面呈现出欣欣向荣的局面，让大家看到希望，看到前景，令人信心十足。我认为这句话已经透露了上述分析出的重要信息，他所说的“大家”意味深长！当地报纸还强调，邓朴方在渝期间，市领导薄熙来、黄奇帆、邢元敏、马正其、范照兵、刘隆铸或陪同考察，或参加座谈、看望。这种表述，对一个全国政协副主席来说，也是异乎寻常的。 <br />
<br />
<br />
