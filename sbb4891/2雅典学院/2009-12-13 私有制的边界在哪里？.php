																											<div id="postmessage_371941" class="t_msgfont">&lt;论民主&gt;第14章-为什么市场资本主义不利于民主<br />
<br />
作者: （美）罗伯特&#12539;达尔<br />
<br />
&nbsp; &nbsp; 如果我们从民主的角度来考察市场资本主义，当我们走近看时，会发现它有两个侧面。正如罗马神话中的门神雅努斯的徽章，它有两张截然不同的面孔。其中一张是友善的，它朝向民主，另外一张是敌意的面孔，它朝向另一个方向。<br />
<br />
&nbsp; &nbsp; 3．民主和市场资本主义被锁入持续的冲突中，每一方都在改变和限制另一方。<br />
<br />
&nbsp; &nbsp; 到1840年，在劳动力、土地和货币方面都已经有自我规制的市场经济在英国已经完全建立起来了。市场资本主义已经全面地战胜了它的敌人：不仅是在经济理论和实践上，而且在政治、法律、思想、哲学和意识形态上也是如此。它的对手，被彻底地击败了。然而，在一个国家中，即使人民拥有像英格兰人民在前民主时代所拥有的力量，如此的彻底的胜利也难以持久!正如它所常做的，市场资本主义为一些人带来了好处；但也正如它所常做的，它又常常为另外一些人带来损害。<br />
<br />
&nbsp; &nbsp; 尽管选举权受到严格的限制，代议制政府的其他政治体制大体上建立起来了。在适当的时问，1867年和1884年，选举权被扩大了；1884年以后，绝大多数男性拥有了投票权。这样，政治制度为有效地表达对不受管制的市场资本主义的反对提供了机会。那些感到被不受管制的市场伤害的人通过向政治或政府领袖寻找帮助以寻求保护。自由放任(Iaissez faire)经济的反对者找到了有效地表达他们不满的途径，包括政治领袖、运动、政党、文艺节目、思想、哲学、意识形态、书籍、期刊和最重要的投票选举。新成立的工党就关注工人阶级的境况。<br />
<br />
&nbsp; &nbsp; 一些反对者只是希望限制市场资本主义，然而其他的人却希望彻底地抛弃它。还有一些折衷的建议方法：现在限制它，以后再消灭它。那些希望消灭资本主义的人从来没有实现他们的企图。而那些要求政府干预和限制资本主义的人却往往如愿以偿。<br />
<br />
&nbsp; &nbsp; 例如在英国，还有在西欧和许多其他说英语的国家，这些情况发生了。在任何一个政府受制于民众抗议运动的国家，自由放任就难以持久。不受政府干预和管制的市场资本主义在民主国家中是不可能存在的，这是由于如下两方面的原因。<br />
<br />
&nbsp; &nbsp; 首先．市场资本主义自身的许多基本制度要求广泛的政府干预和管镧。竞争性的市场，经济实体的所有权、强制性的契约、防止垄断、保护产权――上述的以及其他的市场资本主义的许多方面要完全依赖于政府实施的法律、政策、秩序及其他的政府行为。市场经济不是，也不可能是完全地自我管制的。<br />
<br />
&nbsp; &nbsp; 其次，没有政府的干预和限制，市场经济不可避免地会对一些人造成严重的伤害；并且这些受到伤害或者是预期将受到伤害的人将会要求政府干预。具有自利动机的经济活动的参与者很少会有动机去考虑别人的利益；相反，他们会有很强的动机去忽视别人的利益，如果这样做会使他们自己受益的话。良心很快就会被如下诱人的损害别人的理由所安抚：“如果我不这样做，别人也会这样做。如果我不将我的工厂中的垃圾和废气排放人河流和空气中，别人也会这么做。如果我不出售缺乏安全性的产品，别人也会这样做。如果我不……，其他人也会……。”在一个或多或少存在竞争的经济中，确实是这样，你不做，别人就会做。<br />
<br />
&nbsp; &nbsp; 当损害来自于不受限制的竞争和市场所决定的决策时，问题一定会涌现出来。这种损害能够被消除或是减少吗?如果是这样，这个目标能够以不过多的代价达到吗?当一些人受到了损害而另一些人却受益了，这是经常出现的情况，我们如何判断哪一种是可取的呢?什么是最优的解决方案?或者如果没有最优，那么什么是可以被接受的解决方案?这些决策该如何和由谁做出?决策又如何和通过何种方式被实施?<br />
<br />
&nbsp; &nbsp; 显然，这不仅仅是经济学的问题。它们同样是道德和政治问题．在民主国家中，寻求这些问题的答案的公民将不可避免地转向政治和政府。为了改变一个可能的坏结果而干预市场经济的有效和最容易就范的候选人就是……这个国家的政府。<br />
<br />
&nbsp; &nbsp; 当然，心怀不满的公民能否成功地要求政府干预市场经济取决于许多因素，包括敌对双方力量的相对强弱。然而，历史清晰地表明：在所有民主国家中，‘不受限制的市场已经造成或者预期将造成的损害已经诱使了政府的干预，共同的是改变有可能损害一些公民的结果。<br />
<br />
&nbsp; &nbsp; 在―个以恪守市场资本主义而闻名的国家中，即美国、联邦、州和地方政府干预经济的方式不胜枚举。下面是其中很少的一些例子：<br />
&nbsp; &nbsp; *失业保险；<br />
&nbsp; &nbsp; *老年人年金；<br />
&nbsp; &nbsp; *为避免通货膨胀和经济衰退而制定的财政政策；<br />
&nbsp; &nbsp; *有关食品的药物、航空、道路、高速公路和街道<br />
&nbsp; &nbsp; 的安全法规；<br />
&nbsp; &nbsp; *公共保健、对传染性疾病的控制，对学龄儿童的<br />
&nbsp; &nbsp; 强制性疫苗注射；<br />
&nbsp; &nbsp; *健康保险；<br />
&nbsp; &nbsp; *教育；<br />
&nbsp; &nbsp; *股票、债券和其他证券的出售；<br />
&nbsp; &nbsp; *商业和住宅区的划分；<br />
&nbsp; &nbsp; *设立建筑标准；<br />
&nbsp; &nbsp; *保证市场竞争，防止垄断和其他对贸易的限制；<br />
&nbsp; &nbsp; *强制征收或削减进口关税和配额；<br />
&nbsp; &nbsp; *给医生、牙医、律师、会计师和其他职业人士核发执照；<br />
<br />
<br />
&nbsp; &nbsp; *修建并维护州和全国的公园、娱乐场所和野生动物保护区；<br />
<br />
&nbsp; &nbsp; *限制商业企业以防止或修复对环境的损害；最后，<br />
<br />
&nbsp; &nbsp; *限制香烟产品的出售以减少烟瘾、癌症和其他恶性后果。<br />
<br />
&nbsp; &nbsp; 等等，等等。<br />
<br />
&nbsp; &nbsp; 总结一下就是：没有一个民主国家中会存在或长期存在这样一种市场资本主义：它不受政府为了改变它的负面影响而对它进行的广泛管制和干预。<br />
<br />
&nbsp; &nbsp; 然而，如果一个国家中民主政治制度的存在严重地影响了市资本主义的运行，那么这个国家中的市场资本主义也会严重地响民主政治制度的运作。也就是说，冷箭(causal anow)射向个方向；从政治到经济和从经济到政治。<br />
<br />
&nbsp; &nbsp; 4.由于市场资本主义不可避免地会产生不平等，它将通过政治资源的不平等的分配而限制多元民主的民主潜力。<br />
<br />
&nbsp; &nbsp; 5．市场资本主义对于把民主发展到多元民主的水平是十分有利的。但是由于它对政治平等所产生的不利后果．它对于民主超越多元水平的发展是不利的。<br />
<br />
&nbsp; &nbsp; 由于前面已经表述过的原因。市场资本主义是专制政体的有力的溶解剂。当它把社会中的地主和农民分别转变为雇主、雇员和工人的时候；把仅仅能够生存或有时连这都做不到的没受过教育的农村大众转变为有文化的、有适度安全感的城市居民的时候；把一小撮精英、寡头集团，或者统治阶级对于几乎所有资源的垄断转变为资源更为广泛分配的时候；当一个大多数人无法阻止一小部分人对他们的统治的制度转变为一个大多数人能够有效地联合他们的资源(不仅是投票权)，并因此而能够影响政府以他们的利益而行事的制度的时候――当在市场资本主义的帮助下带来这些变化，随着经济的发展这也是市场资本主义已经和将继续在许多国家中要做的，它充当了革命性的推动杜会主义和政治转型的工具。<br />
<br />
&nbsp; &nbsp; 当非现代化国家的专制政府致力于发展生机勃勃的资本主义的时候，它们也可能播下了导致自身毁灭的种子。<br />
<br />
&nbsp; &nbsp; 但是一旦社会和政治形态市场资本主义改变并且民主制度已然确立，前景就会有了根本的变化。现在市场资本主义引致的资源的不平等造成了公民中严重的政治不平等。<br />
<br />
&nbsp; &nbsp; 是否和如何能够使多元民主和市场资本主义的联姻对多元民主的进一步民主化有利是一个十分困难的问题，对此没有简单的答案，当然也没有简洁的答案。一个国家中民主政治制度同非民主的经济制度的关系在整个20世纪中对民主的目标和实践提出了严厉而持久的挑战。在21世纪，这个挑战将肯定会继续存在。</div>

							
							
							
							
