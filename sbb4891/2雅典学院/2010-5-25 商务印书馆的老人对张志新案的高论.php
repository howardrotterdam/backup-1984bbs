<a href="http://www.wyzxsx.com/Article/Class14/200906/88304.html" target="_blank">http://www.wyzxsx.com/Article/Class14/200906/88304.html</a><br />
<br />
张志新被判死刑的真正原因<br />
<br />
　　我们在对“张志新案”进行重新了解时，发现陈禹山们在编写《一份血写的报告》时有意掩盖了如下重要的事实：<br />
　　1、张志新在公开场合多次书写张贴大量直接针对毛泽东主席的反动标语，包括“打倒毛泽东”、“绞死毛泽东”、“油炸毛泽东”、“千刀万剐毛泽东”等等。<br />
　　2、张志新在公开场合及开会时多次高呼上述内容的口号。<br />
　　3、张志新在她自己的“毛选四卷”上书写了大量的攻击、漫骂毛泽东主席的言论。<br />
　　4、张志新还有攻击、漫骂周恩来总理的言论。<br />
　　5、1973年11月16日张志新与其他犯人参加“批林批孔”大会，当报告人批判林彪推行“极右路线”时，张志新站起来喊“中共极右路线的总根子是毛泽东”等口号。<br />
　　6、1975年第二次判处张志新死刑时，是否符合当时的法律规定？据查，当时判处张志新死刑的依据是1967年1月中共中央国务院发布的《公安工作六条》。&nbsp;&nbsp;《 公安工作六条》第二条规定：“凡是投寄反革命匿名信，秘密或公开张贴，散发反革命传单，写反动标语，喊反动口号，以攻击污蔑伟大领袖毛主席……，都是现行反革命行为，应当依法惩办。”<br />
　　7、1975年判张志新死刑的报批过程是否符合当时的“法律程序”。据查，当时判死刑的审批程序为，基层法院判死刑须层层上报至辽宁省高级法院，辽宁省高院判死刑，须上报最高人民法院核准，在上报最高法院前，按当时的规定必须经省委常委讨论同意，然后以辽宁省革命委员会的名义上报最高人民法院核准。据了解，1975年判张志新死刑的审批过程符合当时的“法律程序”。<br />
　　所以判张志新死刑的真正原因不是因为她反对林彪、“王张江姚”（林彪1971年已死，辽宁省在文革中没有因反对“王张江姚”被判死刑的），不是因为《一份血写的报告》中引用的张志新的那些言论（请对照我当年的言论），也不是因为张志新坚持《一份血写的报告》中引用的她的那些观点（本人也一直坚持自己的观点），而是因为《一份血写的报告》有意遗漏的，张志新用谩骂的语言“恶毒攻击伟大领袖毛主席”，因此触犯了《公安工作六条》，这才是判张志新死刑的真正原因。<br />
<br />
　　当时对张志新的判决是否有效呢？《中华人民共和国刑法》（1979年7月1日第五届全国人民代表大会第二次会议通过，1997年3月14日第八届全国人民代表大会第五次会议修订）第十二条规定：“……本法实施以前，依照当时的法律已经做出的生效判决，继续有效。”<br />
　　因此，我们可以理解，为什么当年判处张志新死刑的法律工作者不愿给张志新“平反”；因此，我们也可以理解，为什么三十多年过去了，据我们所知，当年参与判处张志新死刑的至少几十个人，却没有发现一个人出来表示“忏悔”。<br />
　　所以，我们认为《一份血写的报告》不是一份认真、严肃的按照时间顺序、逻辑过程、因果关系、事实真相形成的“案情报告”，而是政治目的明确的“文艺作品”。<br />
