													<h2>儒学是不是常识――兼论批判鲁迅 捍卫常识的重要性</h2>
						
						
																											<div id="postmessage_842252" class="t_msgfont">儒学是不是常识？---兼论批判鲁迅、捍卫常识的重要性<br />
<br />
一<br />
反儒派认为儒家思想根本就是错误的，误导了中国两千多年，是“有毒的垃圾”。<br />
这且不说它，还有一种“贬儒派”，认为儒家思想没错，但也没有什么价值，是“无毒的垃圾”。某君说：<br />
<br />
“儒教确实啥都没说错，因为它本身就是堆废话，都是些人类文明发展到现阶段应该有的基本常识。你把它解释成自己的圣经都可以。你自己把一堆屎当圣经都与别人没关系，但不要试图教导别人你的圣经就是全人类的圣经。”<br />
<br />
此君之言重心在于指斥儒家“就是堆废话”，可惜自相矛盾起来了，让我听起来仿佛赞美。两千多年过去了，“儒教确实啥都没说错”，而且说的“都是些人类文明发展到现阶段应该有的基本常识。”这是何等了不起。既然是常识，就绝不是“废话”，绝不是“屎”，既然是基本常识，就有相当的普适性，就不能反、反不得。<br />
<br />
说儒学是常识，没有错。《易经》有“变易、不易、简易”三原则，简易就包含简单、平常、常识、常理等意思。真理往往是简单平常的常识。在形而下的层面，在人生、社会、政治实践层面，在外在的伦理道德规范上，儒家说的确实“都是些人类文明发展到现阶段应该有的基本常识”。<br />
<br />
常识者，常理也，常道也。经常经常，儒家经典所说，才是人生、社会、政治的常道，只有常道才是四海皆准、万古不易的中正之道。<br />
<br />
然复须知，儒家又不仅是常识，不仅是普普通通的常识。儒家的仁义、中庸等原则，心物一元、体用不二等思想，大不易把握，于世人可谓“常不识”。在形而上的层面，儒家对宇宙本体、生命本性、人之本心有着最全面最深刻最透彻认知和证悟。熊十力先生指出：<br />
<br />
“儒言形色即天性，又言尽心则知性知天，明心性天三名而实一也。心者，言其为吾一心之主也；性者，言其为吾所以生之理也；天者，言其为吾人与天地万物之统体也。故心即是性，性即是天。吾人能涵养与扩充其本心之德用而无亏蔽，是谓尽心。尽心即性显，性显即吾人当下便是天，即凡所感摄之一一世界、一一境物，亦莫非天理呈现，故曰形色天性也。”《十力语要初续―新论平章儒佛诸大问题之申述》（黄艮庸答子琴）<br />
<br />
对此心体与性体真正能够透彻证悟者，对儒家的精微高明真正能够把握领悟者，<br />
古今中外有几？<br />
<br />
二<br />
不仅一般知识分子及愚夫愚妇“贬”儒家为常识，中外一些思想界哲学家的“大腕”也喜欢这么干。黑格尔在其《哲学史讲演录》第一卷中如是评论孔子：<br />
<br />
“孔子只是一个实际的世间智者，在他那里思辨的哲学是一点也没有的――只有一些善良的、老练的、道德的教训，从这里面我们不能获得什么特殊的东西。”<br />
<br />
这就表现得颇有学术水平。如果去除其中的贬意，说孔子是“实际的世间智者”这句话倒也没错，可惜只知其一不知其二不知其余，管中窥豹，终究是贬低。<br />
<br />
当然，黑格尔绝不是故意要贬低孔子。黑格尔仅浏览《论语》外语译本，岂能全面深刻地领会孔子思想从而与孔子的真实生命及智慧碰头？他对中国的历史及文明发展的路径缺乏深入了解，对孔子具有浓厚东方特色的智慧缺乏必要理解，而且从欧洲中心、西方文化的标准出发，又岂能对孔子、对儒家作出公允的评价？<br />
<br />
且不说诗书礼易春秋诸经，仅凭《论语》所记录的孔子言行，就足以构成孔子丰富深邃的思想了。孔子学说当然自成体系，却不是那种从概念走向概念的、形而上空对空的、封闭的思辨哲学体系，而是形上形下、亦道亦器的具有浓厚东方特色的文化体系。<br />
<br />
这个体系既充满世俗性、科学性、政治性、实践性、经验性，又富有宗教性、神秘性和超越性。这就不是黑格尔、更不是一般知识分子及愚夫愚妇能够了解和理解的了。<br />
<br />
《中庸》说: “君子之道费而隐。夫妇之愚，可以与知焉，及其至也，虽圣人亦有所不知焉。夫妇之不肖，可以能行焉，及其至也，虽圣人亦有所不能焉。”大意是：君子之道广大而又精微，普通男女虽然愚昧，也可以了解，但它最高深境界，即使圣人也有所不知；普通男女虽然不贤，也可以实践，但它的最高深境界，即便是圣人也有所不能。<br />
<br />
“君子之道费而隐”，所以它既是常识，又是“非常识”、“常不识”，两者相辅相成。<br />
<br />
三<br />
儒家的形上（体）与形下（用）一体，其形而上之道完全落实、体现于形下道之器中，也就是说，其超知识、超逻辑境界是建立在常识基础上的。很多人笑《论语》说的不过是些常识而已，他们不知道，这些常识乃是通往“常不识”境界的必要条件。<br />
<br />
所以，要“悟道”，首先要认知常识；要“卫道”，首先要捍卫常识。<br />
<br />
说起来真有点不可思议，我们这个社会不仅匮乏人文常识，而且许多常识遭到了普遍的反对，明显反常的东西反而被视为“正常”，明显偏激或错误的东西反而被视为深刻，以致真知正学无人问津，歪理邪说深入人心。<br />
<br />
儒家强调政治道德，主张“为政以德”，肯定不认同“为政”以不德、以无道、以暴力和欺诈；儒家主张“民为重社稷次之君为轻”，肯定不认同“党为重国家次之民为轻”。防民之口、与民为敌、“毁学校”、文字狱等行为，利益主义、物质主义、jdp主义之导向，“闷声发大财”的科学发财观等等，自然都不为儒家所允许。<br />
<br />
在个人修养和社会道德上，长久以来流行的利己主义、言行不一、见利忘义、无情无义、不诚不信等等不道德现象，都犯了儒家之忌，也犯了文明之忌。儒家常识也是文明常识呀。而国人早已见怪不怪，甚至不以为耻反以为荣。儒家强调的诚信，已经成为迂腐、愚蠢的代名词；儒家反对“巧言令色”，巧言令色成了社会各界特别是政界的“流行色”……<br />
<br />
其实也不奇怪。仁义礼智信五常道（人之常道，政治、社会之常道）都被打倒了，被鲁迅们恶贬辱骂为“吃人”的东西了，那些不仁不义无道无德的东西，能不乘势揭竿而起“沧海横流大显英雄本色”乎？鲁迅不愧为反常识的典型、先锋和标兵。<br />
<br />
道家也有反常识倾向，比如把知识学问视为人生的赘疣，把德仁义礼视为“失道”的成因，把圣贤人物看成比盗贼更坏。但是，道家反德不反道，不仅不反，而且唯道是尊，以道为体----这方面与儒家一致。鲁迅则是无“体”之徒，全面彻底地反仁义道德、反中华文化，与道家性质完全不一样。<br />
<br />
儒家与道家有异，道家尊道遗德、有体无用，儒家道德并尊、体用不二。但在根源处有共同点：都尊道。儒家与鲁迅，无论在枝节上有多少共同点，在根源处完全相反，乃极度矛盾、不可调和的思想天敌。因此，尽管道家亦喜欢反儒，作为出世法的道家仍不妨为辅统；尽管鲁迅不是坏人，骨头也不软，甚至有某些值得学习的地方，但作为精神偶像的鲁迅则必须彻底打倒！<br />
<br />
儒家在，鲁迅就成不了气候、登不了神坛；鲁迅在，儒家就不能够振兴、得不到尊重。鲁迅被当做“中国文化的圣人”的时代，必是真正的圣贤、真正的中华文化遭难的时代。故尊鲁者必反儒，尊儒者必反鲁。目前虽然鲁迅某些文章被撤下课本，但鲁迅的影响依然深广，鲁粉的气焰依然嚣张，有识之士当发扬“痛打落水狗”的精神乘胜追击，直到将神圣鲁迅打回原形，还他一介普通文人的真面目。<br />
<br />
四<br />
或说：西方没有儒家，不也照样文明？<br />
<br />
不错，西方没有儒家，但近现代西方对于各种人文常识从不曾象中国那样进行反对、批判和抹黑，而且，儒家在近现代西方受到相当的尊重和一定的汲取，不少西哲对道德的认识却“天然”地与儒家接近，视道德为文明的核心。近代以来，儒家在母国与在西方的遭遇，恰形成了极为鲜明的对比。<br />
<br />
历史上，以儒家为意识形态的中国的文明程度曾远远高于西方。十七世纪,中国儒家思想传到欧洲,曾有力地推动了欧洲的启蒙运动,促进了欧洲及世界的发展。如有教无类、平等受教育的思想和“民重君轻”、“民惟邦本，本固邦宁”的民本或准民主思想影响最大。<br />
<br />
儒家思想传到欧洲之后，在欧洲掀起了长达百年的“中国文化热”。孔子的思想受到伏尔泰、莱布尼兹等许多思想家推崇、敬佩。伏尔泰认为儒学是最好最合人类理性的哲学。他说“中国文人的宗教(指儒学)是令人钦佩的。他们没有任何迷信和荒谬的传说，也没有悔辱理性和曲解自然。”。美国汉学家顾立雅在他的《孔子与中国之道》一书中写道：<br />
<br />
“在欧洲，在以法国大革命为背景的民主理想的发展中，孔子哲学起了相当重要的作用。通过法国思想，它又间接地影响了美国民主的发展。”<br />
<br />
当然，在西方，儒家不占意识形态的主导地位。人本主义高于神本主义，也高于马列主义，但却低于儒家的仁本主义。在君主时代儒家已经创造了举世无双的文明，在民主时代，儒家必将开辟出新一轮的、高于现代西方文明的仁本主义文明。<br />
<br />
五<br />
一个乏常识并热衷于反常识的人，必是愚昧而颠倒的人；一个乏常识并热衷于反常识的社会，必是愚昧而颠倒的社会。正如风餐露宿网友所指出：“这些常识是中国人二千年前就提出的，结果二千年后的中国人连这些最基本的常识也反，鲁迅就是代表哦，你不觉得是越活越回去了吗？” <br />
<br />
徐友渔说得好：“人们说中国是礼仪之邦，以前也许是，而现在，分明是道德真空之地。中国的道德和文化重建，需要从确认和重申常识常理开始。中国是一个可怕的国度，在古代有指鹿为马的事情发生，在当代有人为说出常识献出生命。在中国，常识并不受尊重，因此它还需要我们说出来，大声地说，理直气壮地说，不知疲倦地说。”（徐友渔《缺失的其实是常识》）<br />
<br />
某种意义上说，一切政治病、社会病、思想病、道德病、人的心病，都是从乏常识和反常识开始的。在这个严重匮乏常识而又以反对常识、批判常识、抹黑常识为荣的时代，中华文化人的当务之急是把许多缺席已久、失踪已久的常识追回来，尊重它、重申它、宣传它、保卫它。这是政治正常化、社会正常化、人性正常化的前提，是建设道德的政治和文明的中国的前提。2010-9-24东海儒者余樟法<br />
