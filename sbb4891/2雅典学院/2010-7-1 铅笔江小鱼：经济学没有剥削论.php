													<h2>铅笔江小鱼：经济学没有剥削论</h2>
						
						
																											<div id="postmessage_714862" class="t_msgfont">几年前移民加拿大的大学同学回来，几位朋友一起宵夜。大家谈起了最近的血汗工厂事情，工作于某保险公司IT部的同学A说，中国工人待遇的确偏低，主要是因为他们单独一个人，谈判能力很低，而工会又没有话事权，被剥削是难免。显然，他认为工人联合起来，组建工会，议价能力会大大增加，对增加收入有好处。<br />
不需我来辩驳，国外回来的同学说了个自己身边的例子。他所在社区原来负责垃圾回收的是一个很大的企业，而最近工会罢工，没人收垃圾，居民怨声载道。有当地华人看有生意，便买了几台小的清洁车，上门收垃圾。结果很快有人找到他，用手枪指着他的头。罢工继续，垃圾依然。<br />
几年不见，我这位同学的观点已有很大的变化，以前的他会认为西方那种工会组织才帮助得了工人。这就是古人所说读万卷书不如行万里路的道理。工会作为一个卡特尔，通过暴力或者政府的支持维持下去，攫取的实际上是一种权力租。而受损的是更多的弱势工人，以及所有的消费者。<br />
现在的问题，是“剥削论”再度甚嚣尘上，包括连耶鲁大学的陈志武教授都认为“选择自由权被限制的交易是非自愿交易，就存在剥削的可能”。选择自由权被限制并不一定是非自愿交易，我想去欧美工作，但是被国籍限制了，我只能在中国工作，薪水远比不上那边相同的工种，然而我和单位签订的合同是自愿的。环境、制度等因素可能会导致起始点的不公平，然而任何情况下的交易，都是增进彼此福利的帕累托改善。监狱里的犯人高价购买香烟，沙漠里的旅行者愿意付出口袋的所有换回一瓶水，电影《2012》里超级富豪花费10亿欧元购买一张诺亚方舟的船票，这些纵使是极端情况之下的交易，都能够改善现有境况。陈教授的错误，是根据交易双方起点和地位平等与否去判断是否存在剥削，而他所认为的“企业给工人的价格低于边际生产率”更是违背了经济学的基本逻辑。<br />
而剥削论的源头是劳动价值论，认为只有人类的劳动才能创造价值，而资本（仅指货币）、土地、和机器都是辅助的生产工具。劳动价值论的一个推论就是剩余价值论，工人通过劳动创造价值，但却没有占有这些价值，多出那部分被资本家占有了，而资本家“不劳而获”，这就是剩余价值的由来。这种错误的观点的起源有其历史局限性，在农耕时代面对的是辽阔土地，只有投入人力劳动才有收获，视野难免受此影响。<br />
我们知道，对资本概念的误解到了欧文・费雪那里已经得到了彻底的澄清。所有能提供收入的都是资本，货币、劳动力、管理能力、经验技术、房屋建筑和机械设备，甚至是甜美的歌声、漂亮的脸孔、出众的身材，这些都能提供收入，都是资本。通过市场交换，反映的是其租值大小。<br />
既然货币是资本，工人的劳动也是资本，这样一般化资本的概念，企业的本质就昭然若揭了，那是一纸合约而已，不同类别的资本组合合约，有人用货币购买了厂房、机器设备、办公用品，有人提供自己的管理才能，有人提供经验技术，还有一帮工人愿意付出体力劳动，他们组织了一家公司。当然，他们合约可能会比较复杂，有些人领计时工资，有些人领计件工资，有些人有固定的工资收入，有些人只能够领取支付了管理费用、扣除了工人工资、税收之后的收入，这个收入有可能很大，也有可能是负数。因为倒闭的工厂破产的企业，大浪淘沙，数不胜数。<br />
顾名思义所谓合约，无合不约，其前提就包含了自愿原则的。陈教授把户籍、城乡差别等制度因素考虑在内，构造了一个不在真实世界的影子价格，认为工人收入低于此，就是受到剥削，这其实和马克思经济学“存在一个虚无缥缈的真实价值、然后价格围绕价值上下波动”说法是类似的。这种观点立足的是改造世界而非解释世界。<br />
如同物理学没有永动机一样，经济学是没有剥削论的。</div>

							
							
							
							
