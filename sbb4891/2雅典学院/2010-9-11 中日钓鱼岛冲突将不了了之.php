																											<div id="postmessage_824574" class="t_msgfont">本周二（九月七日），在中、日之间长期存在领土争执的钓鱼岛海域发生了日本海上保安厅巡逻船和中国渔船碰撞一事。据中方描述，这是中国渔船合法进入中国领海捕鱼而遭到日方非法拘禁。据日方描述，这是中国渔船“入侵”日本领海捕鱼，不服从日方巡逻船的警告并分别撞击两艘日本巡逻船。日本方面强行登船检查，扣押了中国渔船，并以“妨碍公务罪”为由拘禁了船长。<br />
<br />
<br />
事发当日，中国外交部发言人姜瑜便在例行的记者招待会上表示了中国政府对此事的“严重关切”，重申了中国对钓鱼岛的主权。随后，中国的外交反应逐步升级：中国外交部长宋涛和中国外交部部长助理胡正跃分别在二十四小时之内两次召见日本驻华大使丹羽宇一郎，对日方提出强烈抗议，要求日方立即放人放船；中国驻日本大使程永华也紧急向日本外务省提出交涉。事发第三天，姜瑜再次在记者招待会上斥责日本在钓鱼岛运用其国内法为“荒唐”、“非法”，并且明确表示，如果此事得不到合理解决，将必然“影响中日关系的大局”。今天早晨，也就是事发后的第四天，中国外交部长杨洁篪召见日本驻华大使，再次强烈要求日本无条件释放中国渔船船长。<br />
<br />
日本方面的反应虽然言辞温和，但也是软中有硬。日本外务省亚洲大洋洲局审议官北野冲也在事发当天召见中国驻日公使刘少宾，再次强调尖阁群岛为日本领土，称中国渔船为“非法进入日本领海作业”，日方巡逻船登船检查为依法行事。同时也表示对事件的发生“十分遗憾”。首相菅直人在当日晚间表示，“尖阁群岛是日本固有领土”，并且强调这是日方处理此事的原则基础。事发第二天，内阁官房长官仙谷由人一方面呼吁日本国内各界对此事保持冷静，以免事态进一步扩大；另一方面在主权问题上毫不退让，强调必须依据日本法律“严正应对此事”。<br />
<br />
从中日两国当前的政治局势看，双方都不希望此事扩大到难以控制的地步。中国方面必须表现出强硬的态度，这主要是为了应对来自国内的政治压力。在共产主义的意识形态被市场经济的实践所击败和被普通民众所摒弃之后，亟需政治合法性的中国共产党便不时地利用民族主义来作为手段抵御来自西方的普世价值。正因为如此，在与邻国的领土争执上，共产党是难有回旋余地的，假如他们在这一问题上表现软弱，仅有的一点用于装饰的政治合法性便会丧失殆尽了。此举不仅会引起国内民众的不满，而且也会成为党内各派政治斗争的一个导火索。但是中国政府当前还没有实力通过军事手段来取得钓鱼岛的实际控制权。因此中国方面提出的实质性的要求也仅仅是放船放人而已。可以说中国政府在强硬的言词背后实际上还是相当节制的。<br />
<br />
从日本方面来看，其态度相对于中国来说较为温和也是有缘由的。首先是，中国是日本出口产品的大市场，由于领土争执而失去中国这个市场是当前处于经济困境中的日本所不愿意看到的。更为实际的是，虽然中国大陆和台湾从来都没有放弃对钓鱼岛的主权，但是钓鱼岛实际上是在日本政府的控制之中。日本最重要的政治军事盟国美国对日本对钓鱼岛的占领采取的也是默认和支持的态度。因此对日本而言，以实际占有者的姿态对发生的纠纷一事一议从容处理是成本最小收益最大的应对方式了。从过去的情况看，对进入该海域的他国渔船，日方采取的也都是先行扣押，随后放人的做法。因此可以想像，这一次日本政府也会与以往一样，在走完法律程序之后释放中国船长。到头来既给了中国政府一个面子，又能维持其对钓鱼岛的实际控制。</div>

							
							
							
							
