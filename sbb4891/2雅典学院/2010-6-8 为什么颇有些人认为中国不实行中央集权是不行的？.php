																											<div id="postmessage_665063" class="t_msgfont">没想到居然引起了对中央集权概念的争论，发一下维基百科的解释，供参考：<br />
<a href="http://zh.wikipedia.org/zh/%E4%B8%AD%E5%A4%AE%E9%9B%86%E6%9D%83" target="_blank">http://zh.wikipedia.org/zh/%E4%B8%AD%E5%A4%AE%E9%9B%86%E6%9D%83</a><br />
中央集权是一种国家政权的制度，以国家职权统一于中央政府，削弱地方政府力量为标志，是地方分权的对称。这种形式起源于中国秦朝，并在清朝达到顶峰并迅速衰落。这种制度通常与君主专制相提并论。另外，也跟地方权力较大的几种政权体制作为对照。有观点认为现在中央集权并没有消失，而是隐藏于民主政体之下，如今世界上的许多单一制国家仍旧实行中央集权制。在中国，这种政治制度被史学界认为进存在于封建社会和部分专制政体之中。<br />
<br />
产生和发展、衰落<br />
……<br />
随着19世纪中期后大批的中央集权国家半殖民半封建化的开始，中央集权制遭到近代民主制度的强烈冲击。尤其是要求行政、立法、司法三权分立的近代民主政治制度，其理念正是对中央集权的全面否定。中华人民共和国建立后，实行中国共产党宣称代表了民主集中制的人民代表大会制，号称中央集权和地方分权相结合。<br />
特点及表现形式<br />
中央集权制往往包含有如下特点：<br />
<br />
君上大权 <br />
如果是君主专制的中央集权，在中央集权推行的同时，君王也相应地控制了从地方上收回的权利，成为一个宏观上掌握国家机构一切资源的独裁者，这样的做法将导致国家兴衰的主要因素决定于统治者的素质，也往往造成君王信臣掌握大权，出现内部纷争的原因之一。但不可否认的是，这种专职有时候对于一些具有较大争议的问题能够尽快做出决策，避免了繁复臃j的解决方式。<br />
<br />
权力高度集中的行政机构 <br />
这种形式的中央集权也是目前许多中央集权或偏中央集权的政体的特色，一个职权广泛的政府不仅需要打理宏观政策，也需要关注地方上的事件，这就导致中央机构臃肿，职权分位不明。在中国，解决这一实际问题的方式是设立一些政府中的高级实权者如三公九卿，并划分各机构的职权范围（三省六部制）。这样使中央政府的行政效率提高，在唐以前基本上都能够尽快适应社会生产力发展的要求，是这一时期自然经济发展的重要因素。但职权的分工并不是非常清晰，有时帝王为牵制官员的权力故意不分工，防止官员做大。<br />
<br />
[编辑] 作用<br />
中央集权为政府带来了足够的权力调动国家资源，为建立并维护一个统一的国家起到了重要作用，也为农业为主的自然经济带来了所需要的安定环境，促进了古代社会经济的发展，成为一些采用该制度的国家在中世纪强盛的因素之一。同时这种制度带来的权利集中导致官僚机构臃肿，腐败丛生；在经济上促进了农业、手工业的发展，但更多的是对人民思想言论的控制，加剧了不同利益阶层的对立。而国家的安危被更多的取决于政府和帝王的素质，导致内乱不断。在现代社会，中央集权能够在国家处于危机时候或者国家社会处于极度贫困时产生较大的作用。而当国家处于和平时期的时候，过度的中央集权反而妨害了社会的发展。</div>

							
							
							
							
