原文链接：<a href="http://songshuhui.net/archives/29656.html" target="_blank">http://songshuhui.net/archives/29656.html</a><div class="quote"><h5>引用:</h5><blockquote>“珠圆玉润，粉面含春”或许是对女性皮肤的最高评价。这里的“珠”，说的是珍珠。珍珠粉与美容的关系由此可见一斑。<br />
<br />
早年珍珠稀见罕有，一直被人们视为奇珍。现在我们知道，珍珠并非贝蚌吸取天地精华凝结而成的“灵物”，而是一种独特的病理现象。现在，人们可以人工营造出生珠环境，从而实现珍珠的大量生产。在我国，珍珠很早就被当作药物使用。三国时的《名医别录》就记有珍珠。李时珍认为珍珠的药效主要在于美肤。因此，现在的很多化妆品甚至保健品中都含有珍珠粉。不过，这种历史悠久的美容物质真的有传说的那么神奇吗？<br />
<br />
碳酸钙是珍珠粉的最主要成分，含量一般在90%以上。很多以珍珠粉为主要原料的化妆品在宣传时，会以此作为主推的亮点。没错，钙对人体来说非常重要，它不但是组成骨骼、牙齿的基本成分，还参与了人体很多关键的生理过程。<br />
<br />
不过，想要通过皮肤来吸收碳酸钙，难度很大。皮肤位于体表，是人体最大的器官，由多层结构组成。最外层由5―10层已死亡的扁平无核细胞组成。它们彼此交错排列，已经脱水，角质层十分坚韧。此外，角质层的表面还有一层由脂肪酸、氨基酸等物质构成的薄膜。这种薄膜就连水分子也很难通过――你就算在游泳池里泡上一整天，也还是会感到口渴。<br />
<br />
当然，借助一些角质层较薄的区域以及皮肤上的毛囊、皮脂腺和汗管等结构，皮肤依然具有吸收外界物质能力。在一些动物实验中，研究者观察到可溶性的钙盐能通过小鼠腹部皮肤被缓慢吸收。通过新的技术，我们可以将珍珠粉的粒度控制在100纳米以下，和人体毛孔的直径已经相差无几，以方便珍珠粉成分的吸收。但碳酸钙是一种极难溶于水的物质，即便经皮进入体内，钙元素无法形成离子状态，依旧无法发挥其生理功能。何况在人体之内，钙离子也不是“多多益善”的。<br />
<br />
硬角质蛋白是珍珠的另一种主要成分，含量大约在2%~8%之间。人的指甲就是硬角质蛋白的一种。在传统的珍珠粉生产工艺中，一般是先将珍珠在水中煮沸多日直到其软化，然后再在水中磨细或直接将珍珠加水用粉碎机破碎后再用球磨机在加水状态下粉碎，然后烘干备用。这个过程并未对珍珠粉的各种成分进行额外处理。在这种情况下，硬角质蛋白想要被透皮吸收实在难于登天。<br />
<br />
近年来，随着工艺的进步，珍珠的酸解法和酶解法逐渐发展成熟。在这些工艺中，硬角质蛋白被分解为小分子的氨基酸。有研究者分析，分解产物中含有数十种氨基酸成分。相对而言，氨基酸的透皮吸收较为容易，但这些被吸收的氨基酸并不能直接为皮肤细胞所用，后者所需的营养都是由血液循环提供的，也就是说上述氨基酸同样需要经过血液循环流遍全身，最后才能为皮肤细胞所利用。所以，靠外敷珍珠粉吸收氨基酸以取得“美容”的效果，实在有些吃力不讨好，要想实现此种营养皮肤的目的，不如多吃几个鸡蛋，物美价廉。<br />
<br />
珍珠所含的微量元素也常常成为“美容”噱头的来源。珍珠粉所含微量元素在0.05%到1%之间，包括铁、铝、镁、锌、硒、铜、锰、锗、钾和锶等。这些元素体内发挥各自扮演了重要角色，不过和珍珠粉的其他成分一样，微量元素的摄入同样存在效率问题。何况，珍珠中并没有什么罕见成分，只要做到饮食均衡，我们就可以摄入到充足微量元素。根本无需大动干戈，外敷珍珠粉。<br />
