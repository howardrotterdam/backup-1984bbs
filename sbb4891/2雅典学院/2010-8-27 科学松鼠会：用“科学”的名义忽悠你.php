<br />
果壳里的牢骚<br />
土摩托 @ 2010-9-7 1:04 阅读(394) 推荐值(53) 引用通告 分类: 未归类<br />
周日在上海出差，顺便去旁听了科学松鼠会主办的“果壳时间”第一场科学公益演讲。这次活动组织得非常好，我先后收到过3封确认短信和1次确认电话。每一位参加活动的人都能领到一份资料和一瓶水，小剧场设施也很先进，音频和视频都做得很到位，主持人也竭尽所能活跃气氛，虽然很多言辞显得非常幼齿，但也许他们要的就是这种感觉，我也没话说。另外，这样的活动能够做到完全免费，必须感谢兴业全球基金的支持。<br />
<br />
但主角一上场，问题就来了。第一位上场的是上海交通大学生命科学技术学院教授赵立平，讲的是肠道菌群对健康的影响。这个题目我很熟悉，背景介绍部分还蛮有趣的，但接下来他简单地讲了讲自己的研究，然后就开始向观众兜售自己那套养生方法（说白了就是一种改良了的素食主义），并用自己的亲身经历做例子。可是，这个“慢性病的肠源性学说”只是一个学说而已啊，还没有被科学界普遍接受呢！赵立平把它当做事实加以介绍，却没有介绍不同的观点，我觉得不妥。而且，为了支持他的观点，他还说人体内的细菌分为好与坏两种，好细菌吃植物，坏细菌吃动物，这个说法有点不靠谱吧？<br />
<br />
另外，他还提到中医的“药食同源”，认为中医那套东西和他的学说相吻合，比如扶正祛邪就是把有益菌扶起来，把有害菌压下去。他甚至还暗示说中医之所以认为丹田很重要，就是因为丹田处恰好是肠道菌群的聚居地，这就有点搞笑了。当然他并没有公开支持中医，科学松鼠会还不至于堕落到这个地步。我觉得他之所以这么说，是为了讨好两方面的观众。但是我个人认为，这种和稀泥的做法恰好把两派的人都得罪了。<br />
<br />
没想到，赵立平的演讲还算是4人当中最好的，接下来出场的是中国科学院神经科学研究所研究员仇子龙，题目是自闭症。这个题目我也很熟悉，但他并没有在此病的病因和遗传性，甚至是所谓的“与疫苗的关联”等听众应该感兴趣的问题上花费时间，而是迅速地跳到了他本人的领域-自闭症谱系障碍疾病的分子机理研究。这项研究当然很重要，但我相信普通听众是不会领悟到这项尖端研究的意义的。总之我感觉这场演讲完全不是为普通听众准备的，而只是为了介绍自己的研究成果而已。<br />
<br />
第三个出场的是中国科学院上海硅酸盐研究所生物材料与组织工程研究中心主任常江，他犯了和仇子龙同样的毛病，基本上就是在宣传自己研制的一种生物陶瓷活性材料，但对于这种材料到底好在哪里，为什么好，和其它材料相比优缺点在哪里……等等关键问题都一笔带过，我听完后对这种材料的特征还是两眼一抹黑。<br />
<br />
最后一位出场的是北京大学心理健康教育与咨询中心总督导徐凯文，讲的是灾难发生后的心理干预。因为我对心理学有偏见，所以我不想多说了，只说一句话：听完他的演讲，我对心理学的偏见更深了。另外，他其实也是在宣传某个心理咨询组织。<br />
<br />
总之，听完这场演讲，我感觉像是走进了一个布置精美的厨房，锅碗瓢盆一应俱全，油盐酱醋都是最好的牌子，但端上来的却是4盘注水肉，这还怎么做出好菜呢？<br />
<br />
必须说明一下：对于这4位演讲者的科研水平，我没有资格评价。我只能评价他们的演讲水平，而我认为他们都不及格。我觉得组织者没有想清楚观众到底是谁，想从演讲中得到什么，所以请来的这4位老师都是某学科的专家，对自己的专业也许很在行，但缺乏大局观，不能胜任科普的工作。其实，如果他们能把自己的专业讲好讲透，我个人认为也是很值的，我也很愿意听，但松鼠会肯定又不愿意把“果壳时间”做成一个只有专业人士才能听懂的演讲，也是就两头不靠了。<br />
<br />
从厨房的精美程度来看，松鼠会是非常用心的，选择的厨具都很到位，但为什么却在食材上出了岔子呢？我能想到的一个理由就是中国缺乏高水平的科学家，而且中国科学家也非常缺乏科普精神和经验，松鼠会也是巧媳妇难为无米之炊。<br />
<br />
我这次来上海参加了气候变化媒体论坛，听到了来自国外的科学家做的演讲，水平太高了！听那种高水平演讲，真是一种身心愉快的体验。相比之下，中文科学演讲的水平之差也就显得格外突兀。面对这种贫瘠的科学土壤，我有时真觉得很无奈。从这方面说，科学松鼠会的精神我很佩服，也真心希望他们能做得好，让中国老百姓早日听到一流的科普演讲。<br />
<br />
忍不住再补充一点：演讲结束后两位松鼠会成员在舞台上泣不成声，让我感觉非常奇怪。松鼠会为什么要学“感动中国”，走煽情范儿呢？如果说两位组织者确实感动得必须哭出来，那我就更不理解了：组织一次普通的科普演讲，至于的吗？恕我直言，我觉得松鼠会成员出现这种情绪，说明这个组织已经开始有点准宗教的意思了。这类组织的特点就是其成员相信自己在做一件特别伟大的事情，互相间都互称兄弟姐妹，并对这种“亲密感”感动得一塌糊涂。<br />
