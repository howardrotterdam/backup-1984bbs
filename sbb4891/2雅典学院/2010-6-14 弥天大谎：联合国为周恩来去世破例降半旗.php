																											<div id="postmessage_770832" class="t_msgfont">关于“下半旗”，《辞海》中列有专门条目，是这样解释的：“先将国旗升至杆顶，然后下降到离杆顶约占全杆三分之一处。是公众表示哀悼的重大礼节。”[1]下半旗是当今世界上通行的一种致哀方式。当某个国家的重要领导人物逝世后，以下半旗表示对死者的哀悼。这种做法最早见于1612年。一天，英国船“哈兹・伊斯”号在探索北美北部通向太平洋的水道时，船长不幸逝世。船员们为了表示对已故船长的敬意，将桅杆旗帜下降到离旗杆的顶端有一段距离的地方。当船只驶进泰晤士河时，人们见它的桅杆上下着半旗，不知何意。一打听，原来是以此悼念死去的船长。到17世纪下半叶，这种致哀方式流传到大陆上，遂为各国所采用[2]。从中不难看出，下半旗这一致哀方式自古有之，至今已有近400年的历史。 <br />
关于联合国为周恩来逝世下半旗，早就有不同的文章提及，下面摘引几例： <br />
<br />
总理逝世第二天，联合国总部的联合国旗降了半旗，其余所有会员国的国旗都不升起，这在联合国从无先例（自此，始有联合国降半旗之仪），有的国家提出质问：“我们国家的元首去世，联合国大旗依然升起，中国的第二首脑去世，联合国不仅降半旗，还把其他国家的国旗收起来，这是为什么?”[3] <br />
<br />
1976年1月8日，周恩来与世长辞，美国纽约联合国总部门前悬挂的联合国国旗降了半旗。自联合国成立以来，世界上有很多国家的元首或政府首脑去世，联合国从来没有为谁降半旗，一些国家感到不公平。很多外交官聚集在联合国大厦门前的广场上向联合国发出了言辞激烈的质问。[4] <br />
<br />
参加完当天的会议后，我和我的同事步出联合国大厅，准备返回代表团驻地，继续值班守灵。在等待汽车时，我看见挂满各国国旗的广场上出现了少见的一幕：中国的国旗降至一半，正迎着风雪飘扬。我当时的直觉是，联合国广场上下半旗对周总理的逝世表示哀悼了。这可是一件少有的事情，因为联合国从1945年成立至1976年的31年期间，尽管世界上有不少国家元首与世长辞，这里还从来没有为哪位元首的去世下过半旗。我正想着，只见广场上还有一些国家的代表在议论。我走了过去，想听听他们讲些什么。原来他们尊敬周总理，但不知道联合国总部为何要对周总理作为中国政府首脑(第二把手)的去世下半旗志哀，而他们国家的元首去世时，联合国总部却从来没有下过半旗表示哀悼，为此他们想了解联合国总部是否有了新的规定。[5] <br />
<br />
1976年1月，周恩来总理逝世时，联合国给我们敬爱的周总理降了半旗，这是第一次也是唯一的一次给不是国家元首的一国领导人降半旗以示哀悼。[6]</div>

							
							
							
							
