													<h2>关于联合国为周恩来逝世下半旗的调查报告 作者:CCR</h2>
						
						
																											<div id="postmessage_780586" class="t_msgfont">关于联合国为周恩来逝世下半旗的调查报告 <br />
<br />
作者:CCR <br />
<br />
<br />
目次 <br />
<br />
1 引言 <br />
2 调查的目的 <br />
3 调查要解决的最关键的四个问题 <br />
（1）在周恩来逝世之前，联合国有没有下半旗的规定？ <br />
（2）在周恩来逝世之前，联合国有没有下半旗的实例？ <br />
（3）联合国为周恩来逝世下半旗到底算不算破例？ <br />
（4）联合国秘书长瓦尔德海姆有没有讲过那段话？ <br />
4 调查要探究的八个细节 <br />
（1）会员国、非会员国 <br />
（2）国家元首、政府首脑 <br />
（3）实职、虚职 <br />
（4）正式、代理 <br />
（5）在职、曾任 <br />
（6）正职、副职 <br />
（7）必须、可以、应当 <br />
（8）国家下半旗、联合国下半旗 <br />
5 多余的话 <br />
<br />
<br />
1 引言 <br />
<br />
最近一个时期，互联网上有一篇《联合国为周恩来降半旗的真正原因》的帖子传得沸沸扬扬，让许多人感到真假难辨。针对联合国秘书长瓦尔德海姆讲话的真假，各方观点不一，甚至有人怀疑联合国究竟有没有为周恩来逝世下半旗。为此，我进行了调查，尽最大努力收集了自己能接触到的正反两方面的相关资料，在反复比较、综合权衡的基础上，本着实事求是、为历史负责的态度提出自己的观点，供大家参考。中国将军政要网的陆军准将（网名）已经先期撰写了《对“联合国为周总理逝世降半旗之前因后果”的辩疑》的文章，我会尽量避免和该文重复的部分，大家可以结合起来对照着看。调查报告初稿于2004年1月19日写成，在部分周研会成员和再亭网友中征求意见后，作了必要的、可能的修改，现在形成了下面这篇文字。 <br />
<br />
2 调查的目的 <br />
<br />
（1）搞清联合国为周恩来逝世下半旗的真相，恢复事件的全貌和原貌，披露更多细节。 <br />
（2）通过实例，剖析说明联合国为周恩来逝世下半旗究竟是否破例，是否有特殊性。 <br />
（3）促使大家对这个问题继续作深层次的探究，解决目前仍然存在的疑点和盲点。 <br />
<br />
3 调查要解决的最关键的四个问题 <br />
<br />
（1）在周恩来逝世之前，联合国有没有下半旗的规定？ <br />
<br />
关于“下半旗”，《辞海》中列有专门条目，是这样解释的：“先将国旗升至杆顶，然后下降到离杆顶约占全杆三分之一处。是公众表示哀悼的重大礼节。”[1]下半旗是当今世界上通行的一种致哀方式。当某个国家的重要领导人物逝世后，以下半旗表示对死者的哀悼。这种做法最早见于1612年。一天，英国船“哈兹・伊斯”号在探索北美北部通向太平洋的水道时，船长不幸逝世。船员们为了表示对已故船长的敬意，将桅杆旗帜下降到离旗杆的顶端有一段距离的地方。当船只驶进泰晤士河时，人们见它的桅杆上下着半旗，不知何意。一打听，原来是以此悼念死去的船长。到17世纪下半叶，这种致哀方式流传到大陆上，遂为各国所采用[2]。从中不难看出，下半旗这一致哀方式自古有之，至今已有近400年的历史。 <br />
关于联合国为周恩来逝世下半旗，早就有不同的文章提及，下面摘引几例： <br />
<br />
总理逝世第二天，联合国总部的联合国旗降了半旗，其余所有会员国的国旗都不升起，这在联合国从无先例（自此，始有联合国降半旗之仪），有的国家提出质问：“我们国家的元首去世，联合国大旗依然升起，中国的第二首脑去世，联合国不仅降半旗，还把其他国家的国旗收起来，这是为什么?”[3] <br />
<br />
1976年1月8日，周恩来与世长辞，美国纽约联合国总部门前悬挂的联合国国旗降了半旗。自联合国成立以来，世界上有很多国家的元首或政府首脑去世，联合国从来没有为谁降半旗，一些国家感到不公平。很多外交官聚集在联合国大厦门前的广场上向联合国发出了言辞激烈的质问。[4] <br />
<br />
参加完当天的会议后，我和我的同事步出联合国大厅，准备返回代表团驻地，继续值班守灵。在等待汽车时，我看见挂满各国国旗的广场上出现了少见的一幕：中国的国旗降至一半，正迎着风雪飘扬。我当时的直觉是，联合国广场上下半旗对周总理的逝世表示哀悼了。这可是一件少有的事情，因为联合国从1945年成立至1976年的31年期间，尽管世界上有不少国家元首与世长辞，这里还从来没有为哪位元首的去世下过半旗。我正想着，只见广场上还有一些国家的代表在议论。我走了过去，想听听他们讲些什么。原来他们尊敬周总理，但不知道联合国总部为何要对周总理作为中国政府首脑(第二把手)的去世下半旗志哀，而他们国家的元首去世时，联合国总部却从来没有下过半旗表示哀悼，为此他们想了解联合国总部是否有了新的规定。[5] <br />
<br />
1976年1月，周恩来总理逝世时，联合国给我们敬爱的周总理降了半旗，这是第一次也是唯一的一次给不是国家元首的一国领导人降半旗以示哀悼。[6] <br />
<br />
从以上文字中我们可以理解成，联合国在周恩来逝世之前没有为其他人下过半旗，也没有制定过下半旗的规定。因为都是用的绝对化的字眼，“从无先例”、“从来没有”“第一次也是唯一的一次”等等。“从周恩来开始，联合国才有了为国家元首或政府首脑逝世下半旗的规定”――以前我就是这样认为的。外交部主页上有“联合国对会员国元首或政府首脑逝世，联合国旗下半旗一天，并且不升所有会员国国旗”的规定，但没有写明是从什么时候开始的。曾经有人说《联合国宪章》上就有为国家元首或政府首脑逝世下半旗的规定，联合国为周恩来下半旗只是“例行公事”，而有心人去翻遍《联合国宪章》历次版本，根本找不到这样的条款。因此我一直坚持认为下半旗的规定是周恩来逝世之后才有的。直到最近有人提供了新的权威材料：联合国1947年制定的《旗典》中对下半旗有过规定。 <br />
经调查，1947年，联合国大会通过了联合国旗的设计方案。联合国旗的图案是浅蓝的底色，正中为白色的正式会徽。由秘书长根据大会的指示起草了关于旗帜大小的规定，于1947年12月19日正式颁布了《旗典》，以管理旗帜的使用和维护旗帜的尊严。《旗典》的内容包括悬挂旗帜时应遵守的礼貌，各种具体情况下旗帜的使用和有关制造及出售旗帜时应注意的事项。1952年11月、1967年（不是1976年――笔者注）又进行过修订，此后一直沿用至今[7]。在其中“哀悼”这个章节中对下半旗有明确的规定，共11个条款。参考译文如下： <br />
<br />
a.对于联合国会员国的国家元首或政府首脑逝世的情况，联合国总部、联合国在日内瓦的办事机构以及联合国驻该会员国的办事机构将以联合国旗将下半旗的仪式致哀。 <br />
b.在这种情况下，在总部和日内瓦，联合国旗下半旗的仪式应该在得知逝世消息的当天马上进行；但如果那天旗帜已经升起，则通常不会再降下，而应将下半旗仪式推迟到得知逝世消息的次日进行。 <br />
c.如果由于天气状况或其他原因导致上述程序（b款）无法进行，联合国旗应在举行葬礼的当天下半旗。在特殊情况下，在逝世和举行葬礼的两天，联合国旗都下半旗。 <br />
d．在一个会员国的国家象征人物或国家元首、政府首脑逝世的情况下，除了a款所涉及的外，其他联合国机构，可在与该国外交部的礼仪机构和（或）当地的该国外交公使团负责人接洽后，根据实地的不同情况行使自由决断权。 <br />
e．在联合国秘书长授权下，联合国专门机构的负责人可以在希望遵从该机构所在国的官方悼念而下指令该部门降半旗。他也能授权在专门机构进行官方悼念的任何时候对联合国旗实行下半旗。 <br />
f．联合国旗也可以根据联合国秘书长的特别建议，为与联合国有重大联系的世界级领导人的逝世下半旗。 <br />
g．联合国秘书长在特殊情况下可以决定在任何地方的联合国旗在联合国的官方悼念期内下半旗。 <br />
h．联合国旗在实行下半旗仪式时，应先将旗升至旗杆顶间歇，然后再降至半旗位置。而在当天降（收）旗的之前，也要先将旗升至杆顶再降旗。 <br />
i．联合国旗下半旗时，会员国国旗一律不升。 <br />
j．只有在联合国秘书长命令之下，方可对葬礼队列中升有联合国旗的旗杆装饰褶皱彩带。 <br />
k．当用联合国旗覆盖棺木的时候，旗帜不允许低于墓穴或接触地面。 <br />
<br />
由此我们可以得出结论：联合国在周恩来逝世之前是有下半旗的规定的。 <br />
<br />
（2）在周恩来逝世之前，联合国有没有下半旗的实例？ <br />
<br />
对于联合国为周恩来逝世下半旗这件事本身，很少有人怀疑。不过，也有人持有异议，理由是这样重大的消息在当时的《人民日报》上竟然没有报道，时隔26年直到2002年1月才由吴妙发披露出来，有的文章播发时附的联合国下半旗的照片没有注明拍摄日期；有人向联合国档案馆发邮件询问是否有文件记录1976年为周恩来降旗的决议，联合国档案馆的答复是没有找到；云云。当然，这个疑问不难解释。当时的《人民日报》的确没有报道，但1976年1月12日《参考消息》第三版上披露了这条消息： <br />
<br />
《联合国将持续数天下半旗哀悼周总理逝世》 <br />
【路透社联合国一月九日电】为了哀悼中国故总理周恩来，联合国旗今天下半旗，联合国会员国的国旗没有升起，一位联合国发言人说，这种哀悼方式将持续数天。在通常情况下，只是在一位重要的国家政治人物举行葬礼的日子下半旗。 <br />
<br />
这条当时的消息明白无误地说明了联合国为周恩来下半旗是千真万确的事实，绝对不容否定。 <br />
现在要讨论的是周恩来之前究竟有没有人享受过联合国下半旗的殊荣，到底是不是事实上的第一个。 <br />
《旗典》1947年就制定了，对下半旗也有明确规定，但不能断定它正式实施了，除非有实例证明。打个不一定恰当的比方，国务院1988年就已经制定颁布了《中华人民共和国筵席税暂行条例》，但至今没有执行，也没有宣布作废。所以，大家可能有这样的想法，会不会《旗典》1947年制定以后关于下半旗的规定一直没有实施，是1976年周恩来逝世以后正式开始的。我曾经通过各种途径寻找周恩来逝世之前有没有联合国下半旗的实例，但一直没有找到。这里有三种可能，第一种可能是没有国家元首、政府首脑或其他符合条件的人去世，不需要下半旗；第二种可能是有国家元首、政府首脑或其他符合条件的人去世，但事实上没有下半旗；第三种是有这样的实例但国内没有报道无从知晓。根据现在掌握的资料来看，前两种可能都可以排除了。因为已经有人举出了实例，1948年印度的国父――“圣雄”甘地遇刺、1953年苏共中央总书记、苏联部长会议主席斯大林病逝、1968年美国著名黑人民权运动领袖马丁・路德・金被暗杀，联合国当时都为他们的去世下了半旗，而且提供了详细的资料来源。还有人披露联合国还为前任秘书长哈马舍尔德下过半旗，但不是逝世当时（1961年），而是逝世1周年时（1962年）。网友陆军准将在《对“联合国为周总理逝世降半旗之前因后果”的辩疑》一文中已有详细严谨的论证，这里不再赘述。如果不强调逝世者的具体职务或身份，这些例子中只要有一个最终得以证实，那么周恩来就不是联合国下半旗的第一人。我们必须尊重历史事实，没有必要硬去争什么“唯一”、“第一”。 <br />
需要补充说明的是：有人披露，1945年4月美国总统罗斯福逝世，联合国也为他下了半旗。我个人观点，这种说法不能成立。当时联合国尚未正式成立，不存在“会员国”的说法，只能说“当时参与筹建联合国的国家”；联合国旗还没有制定，即使下半旗也是下的各国国旗。这两个最基本的要件都不满足，谈不上联合国为他下半旗。不过，这一因素现在对于判断“周恩来是否联合国下半旗第一人”并不构成实质性影响。 <br />
<br />
（3）联合国为周恩来逝世下半旗到底算不算破例？ <br />
<br />
既然周恩来逝世之前，已有《旗典》关于下半旗的规定，又有其他政治人物下半旗在先，那么，联合国为周恩来下半旗还算不算破例呢？从现在所能接触到的资料来看，尽管《旗典》早就有规定，但似乎并未有效执行。如果按照《旗典》的规定，有很多人都符合条件，联合国在他们逝世时是应该下半旗的，例如1952年英国国王乔治六世、1963年美国总统肯尼迪、1964年印度总理尼赫鲁等，但目前没有找到更多相关的历史资料予以证实。当然目前没有找到，并不代表肯定不存在，现在就下这样的结论未免武断。我想，联合国下半旗毕竟不是“家常便饭”，起码的新闻报道价值还是有的，只要是客观存在，总会留下一些痕迹，最起码在逝世者所在国不会湮灭。或许，随着接触资料的范围扩大，大家还会披露出这样的事例。不过，我个人估计，这样的实例不会很多，至少不是每个国家元首或政府首脑都享受了这一殊荣。 <br />
周恩来逝世时行政职务是国务院总理，即政府首脑，符合《旗典》关于下半旗的规定a款。周恩来也不是联合国下半旗的第一位总理，因为斯大林是苏联部长会议主席（即苏联总理）。但是斯大林是苏联的第一把手，更有“二战三巨头”之一的特殊身份，周恩来无论从哪个角度都不能算是中国的第一把手。甘地、马丁・路德・金、哈马舍尔德三人又都不具备国家元首或政府首脑的身份，而且都是非正常死亡，这一点上和周恩来没有可比性。简单地说，在周恩来逝世之前，很多国家的第一把手去世时联合国都没有下半旗，也没有确切证据能够证实哪一个国家元首逝世时享受了这一殊荣。说“家常便饭”就更不对了，远的不说，同样在1976年1月逝世的马来西亚总理拉扎克[8]，联合国为他下半旗的记录就没有找到。从实例的比较中可以看出的确是不同一般的。 <br />
联合国为周恩来逝世下半旗究竟是否破例，首先要明确破例的概念。严格意义上说，超越文字规定的做法才算是破例，而《旗典》已经把所有的情况都考虑到了，就不存在破例的 说法了，只是执行不同的条款而已。极而言之，哪怕为任何一个人下半旗一年都不算破例，因为g款规定：“联合国秘书长在特殊情况下可以决定在任何地方的联合国旗在联合国的官方悼念期内降半旗”。现在所说的破例是指突破a、b、c三款的做法，这三款综合起来就是：为会员国国家元首或政府首脑下半旗一天或两天，否则就是破例，当然是我重新定义了破例的概念，大家也应该能够认同。 <br />
按照《旗典》的规定，通常下半旗只有一天，即得知逝世消息当日、得知逝世消息次日、举行葬礼当日这三天中的一天。在特殊情况下，下半旗两天，即得知逝世消息当日、举行葬礼当日这两天，或者得知逝世消息次日、举行葬礼当日这两天。从字面上理解，下半旗两日就已经很不容易了。而联合国为周恩来逝世下半旗执行的是g款，周恩来逝世的悼念期根据中央中央、国务院通知和周恩来治丧委员会公告，从1976年1月9日发布讣告开始至15日举行追悼会结束[9]，在这持续的7天里联合国每天都下半旗致哀，这一项更是无人可比，实在太特殊了。有人提出质疑，1976年1月12日《参考消息》第三版的报道只说联合国下半旗“这种哀悼方式将持续数天”，没有具体写明是7天。“数天”究竟是几天？三天以上都可以算。说7天的依据何在？是否信口开河，高估了？我说7天的依据是《举世景仰的伟人周恩来》中的一句――“周恩来总理逝世的噩耗震动了联合国，联合国当即决定下半旗一周表示哀悼，给予这一位为世界的和平与进步作出杰出贡献的伟人以独一无二的殊荣”[10]，暂时还找不到更权威、更直接的材料，希望有人加以佐证。 <br />
按照《旗典》的规定，降半旗时间应该在得知逝世消息的当天。但如果当天，联合国旗帜已经全升，则在正常情况下不会降旗，应将降旗仪式推迟到次日进行。有人说，当时联合国总部在获悉周总理逝世的消息后，是把当日已经升起来的旗帜降下来而不是次日再升半旗。如果真要是这样，的确是突破常规。我们先要搞清联合国得知周恩来逝世的消息是什么时候。虽然没有直接证据，但我们可以从其他材料中合理推断。请看下面这则报道： <br />
<br />
【美联社东京一月八日电】（北京时间一月九日三点零五分）日本无线电通讯社星期五报道，中国总理周恩来已经逝世。终年七十八岁。无线电通讯社说，中国官方的新华通讯社在中文广播中宣布了这一消息，东京星期五清晨收听到了这一广播。[11] <br />
<br />
这是目前为止我接触到的资料中，提到的新闻媒体发布周恩来逝世消息时间最早的。我们完全有理由推定西方的传媒几乎是同步的。北京时间（东八区）1月9日凌晨3：05分，联合国总部所在地纽约（西五区）和北京的时差是13个小时，所以当时应该是1月8日下午2：05分。当日的联合国旗早已升起了，按《旗典》规定下半旗仪式推迟到次日（9日）进行。至于联合国当即把已经升起来的旗帜降下来的说法，没有第一手资料可以证实，笔者也不认同这一观点。 <br />
有人说，下半旗的程序是首先由本国政府或是本人所属的组织或团体向联合国有关机构提出申请, 联合国秘书长可依据死者生前的贡献，再决定联合国是否降半旗以示尊崇与追悼。例如，1997年邓小平逝世时，中国驻联合国大使秦华孙是在当地时间2月19日下午与北京联系后，向联合国提出为邓小平去世降半旗的请求的。而为周恩来下半旗，中方并未提出申请，是联合国秘书长在听到消息后主动临时决定的。这种说法是否准确，中方当时究竟有没有提出申请，现无从查考，但我倾向于没有提出申请。 <br />
联合国旗通常情况下每天早上8点钟升起，下午4点钟降落[12]。据有关资料披露，1976年1月9日为周恩来逝世下半旗是凌晨5点[13]，提前了3小时。但是，这一说法的可靠性无法验证。 <br />
<br />
从那天开始到一月十五日（他的葬礼举行的日子），甚至以后更长的时间里，几乎所有国家中所有重要的人物，都对他的逝世发表了悲痛的声明或谈话；许多国家下半旗志哀，联合国旗也下半旗，同时没有升起联合国会员国的国旗。这在这个世界组织中是极其罕见的事情。无数唁电和唁函飞向北京；各国报纸以显著地位刊载了这惊人的噩耗，有的报纸整版套上了黑框；无数电台播放这悲痛的消息，有的还播放沉痛的哀乐；周恩来的照片和影片资料，反复地出现在书刊报纸和电视屏上；无数普通的群众涌向当地中国使馆和驻在机关，表示自己的哀悼，悲痛汹涌在他们心里，热泪洒在他们胸前，全世界都为他哀歌。[14] <br />
<br />
当时和中国建立外交关系的国家是107个[15]，而有130多个国家和政党领导人先后发来唁电、唁函[16]，说明有一些尚未和中国建立外交关系的国家也表示了哀悼。周恩来获得了敌、我、友三方一致的高度评价和世界各国的广泛赞誉，这种超越政见、超越时空的哀悼，是非常罕见的。 <br />
综合起来看，尽管周恩来不是联合国下半旗的第一人，但国际社会的悼念规模和方式无论从广度还是深度都表现出异乎寻常，远非正常的惯例可比。应当说周恩来总理享受了极其特殊的荣誉，这一点丝毫不夸张，尤其是联合国持续七天下半旗，在这一点上说空前绝后决不为过。 <br />
<br />
（4）联合国秘书长瓦尔德海姆有没有讲过那段话？ <br />
<br />
这个问题是目前争议最大的地方。 <br />
有人认为这是“编造出来的”，“夸张的报道”，“画蛇添足”，“可信性极低，典型的报告文学”，“和‘总理遗嘱’一样”，“一看就是那种‘煽情网文’，不值得当真”，甚至指斥为“弱智的谎言”，“挟洋自重”，也有人对此不屑一顾，认为即使是真的，也并不值得夸耀。他们的理由是： <br />
――吴妙发回忆文章的可信度差，当时中国人口没有10亿，他写的其他文章也有好多失实之处，推断这个故事也是他捏造的。 <br />
――联合国秘书长不会说那样不严肃的话，没有存款不见得是好事情，没有孩子也不是周恩来的本意。 <br />
――没有存款？×××、×××都能做到。没有孩子，在任何一个国家都是很遗憾的事，甚至于会有不好的联系（始作俑者，其无后焉），怎么可能反而当作先进事迹大吹大擂。 <br />
――即使真有这样的事，瓦尔德海姆肯定也是一本正经的说“周恩来是一位伟大的政治家，为世界的和平与进步作出了巨大的贡献，因此我们……”这类官话，他思维有问题吗，说那样的话送给记者当靶子，去刺激别的国家，联合国秘书长可是选出来的。 <br />
――难道联合国秘书长会影射其他会员国的元首或首脑都是贪污犯？更何况联合国也鼓励合法的劳动所得，政府首脑也是雇员，秘书长会说出这样公然违反劳动法的话？ <br />
――“他没有子女”这能说明什么？这位秘书长会疯狂到鼓励不孕主义？看看联合国人权大会每年对中国的计划生育政策说三道四，你还会相信他会鼓动不孕主义吗？ <br />
………… <br />
还有一些更激烈的看法，涉嫌人身攻击，恕不引用。 <br />
那么，真实情况又是怎样的呢？中共中央文献研究室研究员安建设发表在《党的文献》2003年第2期上的文章应该足以能够说明问题： <br />
<br />
还有一件事情值得一提。过去国内曾有这样的传说:1976年1月,为了在联合国总部“破例”给已故的周总理下半旗,联大秘书长举出“三条理由”,用来说服在场的人。26年之后,这件事才基本上得到证实。据当年在联合国总部工作的一位中国外交人员回忆:当周总理去世的消息传至纽约联合国总部时,顿时引起了巨大震动和深切哀悼。从各国常驻代表团大使到一般外交官,从联合国秘书处高官到一般工作人员,都用自己的方式向这位伟人表示了崇敬之情。“更为感人的是,联合国还特地下半旗哀悼周总理。这是联合国建立50多年以来罕见的事情。当年我站在联合国广场聆听了秘书长瓦尔德海姆对此作出的既感人又意味深长的讲话。他说:‘为了悼念周恩来,联合国下半旗,这是我决定的。原因有二:一是中国是一个文明古国,她的金银财宝多得不计其数,可她的总理周恩来没有一分钱的存款!二是中国有10亿人口,占世界人口的四分之一,可是她的总理周恩来,没有一个孩子!你们任何国家的元首,如能做到其中一条,在他逝世之日,总部将照样为他下半旗。’讲完这番话,我见他转身扫视了一下广场,尔后返回秘书处大楼。这时广场上先是鸦雀无声,接着响起雷鸣般的掌声……”[注] <br />
[注]吴妙发:《周恩来逝世震惊世界联合国破例下半旗》。2002年1月8日《人民日报》。[17] <br />
<br />
需要指出的是：虽然还是引用吴妙发的文章，但可信度明显提高了，它是登载在人民日报上，向全世界公开的，不再仅仅是民间传说了，否则肯定会有其他国家的外交官来质疑这件事，似乎还没有听到当事人的否认。可惜诺大的中国外交团只有吴妙发一个外交官员来证明，的确显得苍白了一些。据悉，瓦尔德海姆现仍健在，哪天他自己写篇回忆录证实一下更好。还有当时中国驻联大代表团大使黄华应当也是一个知情者。中央文献研究室是研究中共领袖人物的权威机构，他们在正规的学术期刊上引用这一说法，至少说明他们是审核过而且是认可的，他们说是“基本上得到证实”，还是比较审慎的。同时，外交部网站、新华社网站等权威部门都认同这一说法[18]。最近，江苏省周恩来研究会会刊《觉悟》创刊号上刊登了原周恩来外事秘书、外交部办公厅副主任陈浩的文章，称“解开了联合国为周恩来逝世下半旗之谜”[19]。所有这些，我都没有理由不相信，更没有理由说这些研究部门、专家和当事人为了某种特殊目的在刻意编造或者以讹传讹。 <br />
吴的文章，有人说可信度低，并以他写的其他回忆文章中失实较多来证明这一点。我个人观点，如果这些失实属实，说明他在写文章时确有不当之处，但是这些失实不是他的个人经历，是摘引了其他不可靠的文字或进行了不谨慎的评论。在写到他自己的亲身经历时还没发现什么问题。因此，还是应该相信他说的这段话。比如毛毛写《我的父亲邓小平》，如果有些事情不是她的亲身经历，她会引用一些现成的背景材料或别人写的东西，保持事件的连续和完整，一旦引用的材料本身有偏差，容易让人怀疑她整本书的可信性，但事实上她写自己直接经历的事情还是真实的。再如毛泽东孙辈写的关于毛泽东、毛岸英的书，可信度究竟如何，伟人去世时作者尚未出生或者年幼，叙述时肯定是参考了别人的东西，万一有假的，我们不能说作者自己和毛岸青、李敏交往的部分也是假的。不过，我个人认为，写文章尽量写自己的亲身经历，首先主观上不能去造假，不论出于何种目的都不足取，再者引用别人的东西一定要仔细考证。 <br />
鉴于周恩来在国际社会的崇高威望，联合国秘书长发表悼念谈话完全在情理之中，但具体什么内容，版本很多，有些是口头流传的过程中经过人为加工的，以致使人生疑。我小时候听到的不是“两条理由”，而是“三条理由”，正如安建设的文章里所提到的那样。这“三条理由”的内容是： <br />
<br />
世界上有哪一个国家的总理终生只有一个夫人？ <br />
有哪一个国家总理终生受人民爱戴？ <br />
在国外银行无一分钱存款？[20] <br />
<br />
