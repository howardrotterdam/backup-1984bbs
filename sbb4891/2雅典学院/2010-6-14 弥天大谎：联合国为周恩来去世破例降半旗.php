													<h2>弥天大谎：联合国为周恩来去世破例降半旗</h2>
						
						
																											<div id="postmessage_681743" class="t_msgfont">在中国，总会流传着大量的精彩故事。其中，“联合国破例为周恩来降半旗”就是我这代人最熟悉、最感动的一则故事。当时，我对这个故事的真实性坚信不疑。不过，触网后，很快就看到有网友撰文置疑这个故事了，于是，就动摇了我的坚信，最后，由动摇到怀疑、由怀疑而最终再次走向了坚信。只是，坚信这个故事是假的了。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;谎言重复一千遍就成了真理，据说是希特勒的名言。这句话本身是不是真理呢？是。至少在信息封闭的社会里是。这是我这一代人见证了的事实。不过，在言论开放的社会里，谎言是很难被重复成真理的。言论开放的社会不畏惧谎言，也不畏惧事实。大家只要稍微仔细观察一下，就能发现，只有在言论封闭的社会里，才会造成既恐惧事实，又恐惧流言的古怪现象。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;中国今天是言论开放的社会吗？不是。中国今天是言论封闭的社会吗？也不是。中国是正由封闭走向开放的社会。大量的谎言，在完全封闭时期，被认为是真理的谎言，正在被开放地言论证伪，这是中国的现状。于是，我们就能发现一些尖锐的对立，一些人认为是谎言的东西，却被另一些人奉为真理。哪些人是对的呢？这只有靠自己获取信息、开动脑筋来判断了。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;不过，有个简单的建议能帮助你快速地粗略鉴别一下真伪，就是你什么时候获取的信息，什么时候形成的判断。如果是在完全封闭时期获取的信息，并在此基础上形成的判断。则基本上可以判定是错的，这并不是怀疑你的逻辑判断能力，而是因为你那时获取的信息一定是假的，或者是不完整的。在虚假信息或不完整的信息基础上推理，即使是非常正确严密地逻辑推理，也只能出来假结论、错结论。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 每个中国人，都应该向发这个帖子的网友学习。反思一下，到底还有多少谎言，在我们头脑里被当成了真理？<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;借造假来纪念周恩来会适得其反――关于联合国为他降半旗故事<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 在中国广为流传的“联合国为周恩来降半旗”的故事让人听了不禁摇头。该故事说：“1976年1月8日，周恩来逝世时，设在美国纽约的联合国总部门前的联合国旗降了半旗。自1945年联合国成立以来，世界上有许多国家的元首先后去世，联合国还没有为谁下过半旗。一些国家感到不平了，他们的外交官聚集在联合国大门前的广场上，言辞激愤地向联合国总部发出质问：我们的国家元首去世，联合国的大旗升得那么高，中国的总理去世，为什么要为他下半旗呢？当时的联合国秘书长瓦尔德海姆站出来，在联合国大厦门前的台阶上发表了一次极短的演讲，总共不过一分钟。他说：‘为了悼念周恩来，联合国下半旗，这是我决定的，原因有二：一是中国是一个文明古国，她的金银财宝多得不计其数，她使用的人民币多得我们数不过来。可是她的周总理没有一分钱存款！二是中国有10亿人口占世界人口的1/4，可是她的周总理没有一个孩子。你们任何国家的元首，如果能做到其中一条，在他逝世之日，总部将照样为他降半旗。’说完，他转身就走，广场上外交官各个哑口无言，随后响起雷鸣般的掌声。”<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;这故事首先告诉读者，联合国为悼念周恩来总理而下半旗是没有先例的。有一个“破例下半旗”的前提，才可能有后面那精彩的演讲。说故事的人大概想以此证明周恩来享有很高的国际的荣誉。悼念的规格高说明人格高。破格悼念更显得人格非凡。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;可惜这是不真实的。联合国为悼念周恩来总理而下半旗并不是破例之举。联合国于1947年制定了一部旗典（FlagCode）。其中有关致哀的规定是：凡成员国的国家元首或者政府首脑去世，必须在纽约总部和日内瓦的办事处降半旗致哀一天。因此1976年1月为周恩来降半旗只是例行公事。如果不降半旗才真是“破例”。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;没有破例这个前提，后面的故事就显得荒谬了。没有听说有哪个国家驻联合国的使节会为了例行公事而“言辞激愤”。真有这样的不懂联合国的基本规章的外交官的话，我想他们应当感到“羞愧”而不是“激愤”。当然，秘书长也就不需要做特别的说明了。<br />
&nbsp; &nbsp;&nbsp; &nbsp;杜撰出来的那段秘书长的“极短的演讲”以及它产生的戏剧般效果，本身也很有问题。硬要把中国特色的感情推销给洋人，树立一个“老支书”式的联合国秘书长。<br />
&nbsp; &nbsp;&nbsp; &nbsp;周恩来生活俭朴，这已经有很多人证明。但并没有哪一份文件告诉我们说周恩来逝世时的银行存款余额具体是多少。中国没有公布国家领导人个人财产的制度。联合国也从不要求成员国报告其国家领导人的收入情况。秘书长无从知道周恩来个人的财务状况，怎能信口说他存款是多少？<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;“没有存款没有子女”的确是那时中国人民爱戴、悼念自己的总理的一个强烈理由。它符合当时中国的国情，当时中国处在热烈的革命状态。革命革到一贫如洗、革到家庭遭受损失的革命家被认为是高尚的，有这种献身精神的领导人会受到中国人民的普遍敬仰，这是没有疑问的。但是它在中国以外的地方就未必同样令人赞赏。特别是没有子女，更多地被认为是一种人生的缺憾，甚至是一种失败。即便不算什么过错的话，绝没有任何“美德”的含义，真正尊敬爱护周恩来的外国友人是不愿意去议论这一点的，特别是在悼念的场合下更不应该提起。而该故事却让瓦尔德海姆大张旗鼓地推崇这种状况。他似乎还鼓励别的国家领导学习：“你们任何国家的元首，如果能做到其中一条，……”云云。试问这个“秘书长”：做到“身后无子女”这一条是什么好事吗？你打算让那些已经有了子女的各国领导人怎样去“做到”这一条以便赢得你的尊敬？讲出这种话，哪里是什么“秘书长”，分明是个土老冒。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;这种故事如果在“广大工农兵群众”里流传，倒不奇怪。对国外的情况不了解，难免把自己的思维习惯套在外国人头上。奇怪的是一位名叫吴妙发前驻联合国的官员也这样讲，而他“回忆”起这故事的过程也很奇特。他在90年代曾单独或者与人合作出过多部关于乔冠华和联合国的书。在至少两本书中他记载了在联合国悼念周恩来的情况，均无一字提到上述故事，只提到了“中国代表团驻地”降半旗、布置灵堂、以及各国外交官的前来悼念等事情。没有一丝联合国总部降旗以及广场上响起“雷鸣般掌声”这些轰动情节。想不通他怎么能在出书时竟把如此重要的事件给漏掉了。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;而到2002年1月，他却突然回忆出了这个故事。文章登在1月8日的人民网上。他声称“这是联合国建立50多年以来罕见的事情”。还有鼻子有眼地说：“当年我站在联合国广场聆听了秘书长瓦尔德海姆对此作出的既感人又意味深长的讲话。”这实在是令人称奇。在联合国工作多年的吴先生怎么会不晓得“联合国旗典与规则”？1976年时中国所有报刊，包括毛泽东等中国领导人，都讲中国有八亿人口。吴先生怎么会“聆听”到秘书长讲出“中国有10亿人口”这样超前的统计数字？中国出使联合国的人很多，何以别人没有见到，独吴先生见到了如此“罕见的事情”？又何以他要把这“罕见的事情”压了那么多年才肯说出来？<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;看了同为驻联合国外交官的宗道一批评吴妙发的一篇文章后，才对这位吴外交官的轻率妄言习惯有所认识。宗道一指出吴妙发所著的三部关于乔冠华与联合国的书里存在着多处错误、抄袭、和编造现象。例如把周恩来会见瓦尔德海姆的照片说成“美国总统尼克松访华的历史一刻”，他大段抄袭熊向晖等人的书作，添油加醋地编造领袖言论，竟还能把林彪宣读520声明的情形描写成“毛主席那洪亮的声音，坚定的语调感染了在场的每一个人。”等等。宗道一不得不规劝吴妙发：“须知创作不应抄袭，史作不该杜撰。这是常规，并非苛求。”<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;原来是这样一个不负责任的作者，其故事的可信度可想而知了。杜撰情节，用假故事去“缅怀伟人”。这样的做法不能为周恩来增光，反给他添尴尬。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;顺便说，为了证明谁谁伟大，就编造洋人怎样敬仰他的故事。这种事在中国并不少见。在一个声称很讨厌“挟洋自重”的国度里，这做法多少有点讽刺的意味。<br />
