武汉华润啤酒公司工人罢工，已有八人失踪<br />
发布: 2010-6-08 00:04 | 作者: 丑牛 | 来源: 工农之声 <br />
<br />
被立“卖身契” 讨要“卖身钱”　　<br />
<br />
华润工人停工 已有八人“失踪”　　<br />
<br />
公司通牒：依规严惩 绝不姑息　<br />
　 　<br />
<br />
　五月三十一日　，武汉华润雪花啤酒公司职工，为讨要“卖身钱”（国企职工转换合资企业身份补偿金），来到公司门前讨说法，被堵在厂门前，人越聚越多。随即，警车开来，一百多名警察守住公司大门，强行驱散工人。下午四时，导致生产线上的工人全线罢工。<br />
<br />
<br />
警方密集守卫华润公司大门<br />
<br />
华润雪花公司的前身是武汉东西湖啤酒厂，它是上世纪八十年代，由东西湖农场的一个六十多人的老糖厂发展起来的。职工们艰苦创业，白手起家到九十年代就形成了一个现代化规模的国家二级企业。在武汉它赫赫有名，生产的“行吟阁”啤酒，风靡了华中和长江中游地区。　　<br />
<br />
1996年东啤与法国达能集团合资，组建欧联啤酒公司，达能持股51%，东啤持股49%；2002年，达能将股权全部转让华润公司，2003年，华润收购了东啤持有的39%的股权。至此，华润占股90%，绝对控股华润雪花啤酒有限公司。　　<br />
<br />
华润控股后，产量、利润逐年增长，工人劳动条件却不断恶化，工资福利待遇也逐年降低，为维护职工权益，曾几次停工、罢工、请愿。<br />
<br />
<br />
警方和工人发生肢体碰撞<br />
<br />
