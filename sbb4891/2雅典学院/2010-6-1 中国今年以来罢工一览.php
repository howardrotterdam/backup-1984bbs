九江思麦博一群保安打晕一员工<br />
<br />
这家坐落江西九江的台资企业思麦博运动器材有限公司，被认为是全球最大的球类加工厂之一，员工有8千名，生产各类名牌球类，世界级的比赛用的球几乎都是这里生产的，也是这次世界杯足球赛的指点球“普天同庆”的加工企业。<br />
<br />
6月5日星期六下午1点不到，二名女工吃完午饭上班。从九江思麦博西边（2号）门进入厂区，由于没有戴厂牌，被门口一名保安拦住，两名女工想解释，还没等她们说完，保安就对其中的一位动粗。<br />
<br />
该女员工的一名男同事也正好回厂看到这一幕就上前劝说，附近5、6个保安一起冲上来对着该员工一顿拳打脚踢，令他倒在血泊之中。知情者说，当时该人就昏迷不醒，口鼻涌血，生命垂危。事后保安没有叫救护车，也没有做任何救助措施，并对上去围观的员工群众推拉，说没有什么事，说受伤者是自己装的。<br />
