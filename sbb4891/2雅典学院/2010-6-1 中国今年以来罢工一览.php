江西九江一台资企业思麦博，在6月7日因传出保安之前打晕的一名员工已经死亡，厂方不予以解决，导致工人强烈不满，因此数千员工集体维权，将厂里一些设备、门窗毁坏、砸烂，车子掀翻。据最新消磁，厂方发出通告，要求所有员工9日复工。<br />
