													<h2>5毛 国宝对1984的意义及贡献</h2>
						
						
																											<div id="postmessage_750701" class="t_msgfont">这些天1984里面好像来了一些“国宝”“五毛”，我这里加引号的意思是，我并不能确定是不是真的“国宝、五毛”，只不过他们的言论容易被1984网友看成出自五毛，所以本贴以下内容对事不对人，都是针对这些帖子而发，不代表我本人攻击这些ID。<br />
<br />
1、5毛来到1984是好事还是坏事？<br />
绝对是好事，大家现在追求民主自由，但是好像调子很高，实际操作起来比政府还专制。5毛在1984绝对属于劣势群体，对劣势群体的保护是检验一个社群文明的重要标志，可是，我发现1984很多网友对劣势群体的打压程度远远超过政府对异议分子的打压。如果1984追求的宗旨是探讨我们如何走向社会文明、政治文明，5毛来到1984就是一块试金石、磨刀石，所以，其正面作用远远大于负面作用。<br />
<br />
2、可是我就是受不了5毛，怎么办？<br />
很简单，你可以直接忽略掉你不感兴趣的帖子，或者被你认为是5毛ID发的帖子。<br />
<br />
3、我就是不服气，希望把他们轰走<br />
参见第一条，这是你的自由，但是也反映了你自己的文明程度。<br />
<br />
4、5毛很无耻，我难道不能骂他们吗？<br />
当然可以，但是我认为骂人其实是一种非常无能的表现。骂人只有两个结果，第一，说明自己无能、粗鲁和野蛮；第二，激发对方的斗志，让他感觉很有价值。说一个故事吧，骂人的人连小鸡都不如，大家也许知道呆若木鸡的故事，其实那个木鸡是战斗力最强的鸡，它以不变应万变。把其他跃跃欲试的鸡都战胜了。<br />
<br />
5、我骂他们也是一种斗争啊<br />
参见上一条，对。但是很低级。真正的斗争是能够把对方斗垮，我还没有看见一个5毛被骂垮的。被骂垮的人只是君子，所谓君子不与小人斗。你既然说5毛要么领了任务来挣钱，要么是卖身投靠的小人。还采用对待君子的手段，可见你自己很糊涂。<br />
<br />
6、但是与5毛对话很困难，他们不正面回答。<br />
这个倒是，5毛往往闪烁其词，只是不断宣扬自己的观点，不深入讨论问题，而且回避关键问题。我认为比较有效地方式是聚焦问题，你自己也不要说太多，就是盯住一点后，让他们表态yes or no。<br />
<br />
7、不管怎么说，我鄙视5毛。<br />
我不太同意。我不鄙视他们，他们也是在谋生，这是一个职业。而且这个职业也蛮艰难的。我同情他们甚于鄙视。<br />
<br />
8、5毛来1984对我们还有什么好处吗？<br />
当然有，从信息学的角度上讲，你不了解的东西对你是最有价值的，所以，与一个观点差异越大的人对话，对你的收获往往越大。只要你对话的目的不是战胜他们（因为他们的职业所限，他们不会认输的），而是从他们那里开拓自己的思路，那么这种对话对你就有价值。与5毛对话的另外一个价值是可以把你的观点告诉他们。同理，把你的观点告诉一个与你观点相同、相似的人其实价值不大，把你的观点告诉一个与你差异很大的人，就很有价值。<br />
<br />
总之，5毛国宝来1984是有好处的，我希望1984能够成为一个真正容纳不同声音的文明的平台，这个平台对我们，对他们，对政府，对你想帮助的人，都是有好处的。<br />
<br />
如果大家还有什么不同观点和问题尽管拍砖，有时间的时候我会尽量回帖。</div>

							
							
							
							
