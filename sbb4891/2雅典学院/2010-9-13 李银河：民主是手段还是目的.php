民主是手段还是目的？ (2010-09-13 09:36:29)<br />
标签： 杂谈&nbsp; &nbsp; &nbsp; &nbsp; <br />
&nbsp; &nbsp; 写了一篇关于薄熙来的博客，朋友提醒我他这个人很危险。树全国最高毛像，唱红歌，践踏法制，如果他上台，将离宪政越来越远。恐怕是这样的。只有共产党才能治得了黑社会，为什么？就是因为不用法治手段才能治得了他们。当初听说薄回到了法治之前的状态（李庄律师案），但是又听薄说：我手里还有五十几条人命呢。心里又倒向了他这边。一个社会总不能由着黑社会为非作歹吧。<br />
&nbsp; &nbsp; 有时常常感到，是非并不像理论那么黑白分明，有些社会进步也不是按理论上的途径完成的。理论只能指出大致的方向，而具体的事态并不一定是那么戏剧性的。比如说，共产党搞专制，但是它并不是跟皇上一模一样的专制，共产党的政治资源也并不完全是专制的资源，还有社会主义的资源，救济穷人的理论资源。就连胡平都说过：共产党就是只让自己做好事，不让别人做好事。他承认共产党还是做好事的，并不是营一党之私的（薄熙来反腐就是明证）。只不过是不愿意与其他人分权。<br />
&nbsp; &nbsp; 薄熙来现在就是用很夸张的方式（唱红歌，树毛像）炫耀共产党的意识形态资源，利用这个资源办好事（扫黑，反腐）。他的问题是，由于没有民主意识，一旦他开始做坏事（比如说搞文革），人们很难制止他，但是到目前为止，他是在利用历史遗留下来的专制手段做好事。估计扫黑反腐之后，他会做的应该是限制资本，为穷人从富人那里多争工资，降低贫富分化程度了。这也是一件好事，而且是共产党按照自己的宗旨、意识形态资源和权力资源最该做的一件事。问题是要掌握一个度，不要把资本都吓跑了。<br />
&nbsp; &nbsp; 民主到底是手段还是目的？如果说它只是一个手段，那么假定有其他手段比它还有效，就不妨一试。这样做的结果就是比较悬。用专制的手段做好事的时候，有效率，快刀斩乱麻，如扫荡黑社会；用专制手段做坏事的时候，比如反右文革，所有人都对它束手无策，只能眼睁睁看着它十年浩劫。<br />
&nbsp; &nbsp; 如果说民主是目的，那我们就是一心争取宪政，拼命挖掘共产党的民主资源（比如“团结就是力量”那首歌里有一句歌词：向着法西斯蒂开火，让一切不民主的制度死亡），共产党曾经是从国民党手里争民主的，也是五四运动民主和科学精神的意识形态传人。民主在共产党的意识形态里从来都是有正面价值的，虽然不能说是意识形态主流。现在国民党已经从专制独裁改变为民主制了，共产党怎么能不如他们呢。<br />
&nbsp; &nbsp; 在反复思考之后，我还是比较倾向于民主是手段，不是目的。目的应当是人民的生活水平。非洲一些小国搞了民主之后，结果是种族屠杀，民不聊生。所以我们还是应当以中国强大人民生活幸福为目的，用专制手段先达到这个目的也无不可，只是慢慢地稳健地在我们的手段中增加民主的因素，现在不是已经有了村民选举了吗？政协会议上不是已经开始有争论而不是一致举手通过了吗？这样一点一点地改进就好了，也不一定要照搬西方民主制的所有形式（因为就连它们各国之间也不尽相同，有些国家还保留皇室呢），能推进到台湾的民主水平就差不多了，台湾跟咱们同文同种，弄起来比较有把握，不会搞得天下大乱。有些人太浪漫，老想把现有的一切秩序推翻重来，先搞民主制，其他再说，只怕是旧的推翻了，新的又没建立起来，把中国搞得跟非洲一样，那才悔之晚矣。</div>

							
							
							
							
