去，看看太平天国的那段历史，国际社会是不可能诚心实意的支持你的，除非你的力量壮大到可以和现有政府平分半壁江山。现在不是20世纪20年代，没有什么为了意识形态就全心全意支持你的好事 </blockquote></div>第九章．民主革命经费来源<br />
<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;尽管经费并不是民主革命最重要的问题，但一定的活动经费是革命家活动的基本保障，尤其重要的是革命者必须确立并从思想上认知经费来源的基本原则。另外，武装起义后的经费来源与民主革命的经济政策息息相关。因而，革命者在起义前确立自由区军政时期的经济政策就显得非常必要。最后，革命者必须认识到任何革命实质上都是权力和财产的再分配，认识到这点，对用何种方式筹集经费极为重要。<br />
&nbsp; &nbsp;<br />
<br />
第一节．国内捐款<br />
&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp;捐款是一种和平的筹集资金方式，亦即捐款人都是在自愿自觉的情况下支援民主革命。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;一、党费。<br />
&nbsp; &nbsp;党费从来都不是一个革命政党经费的主要来源，尤其是在武装革命时期，每一个政党其所以要求每位党员交纳党费，最主要的目的是增强党员的政党意识，使他们从精神和物质上都维系在革命集团之中。通过交纳党费，党员实际上在体验党的组织活动，而并不在乎党费的多少。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;党费的多少应根据所在地区的生活水平确定。原则上不应超过党员每月收入的十分之一。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;二、党员捐款。<br />
&nbsp; &nbsp;自古以来，不少富有者抛家弃子、卖田卖屋而投身革命。韦昌辉、石达开均为富裕的太平天国信徒。他们变卖家财，投身革命，对拜上帝会起义起了很大作用。我们的民主革命的目的就是要确立私有财产神圣不可侵犯的社会制度，因而我们一定会获得许多遭中共腐败官僚盘剥的私营业者的支持。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;三、党外捐款。<br />
&nbsp; &nbsp;党外捐款应始终坚持一种原则：起义前绝对不向党外捐款人透露革命的目的。因此，这种方式的捐款应主要依赖党员的社会关系筹措。<br />
&nbsp; &nbsp;<br />
<br />
第二节．海外捐款<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;海外有无数厌恶、反对中共的国家、组织和个人。他们对我们追求民主自由之中国无不欢欣，一定会从物质和精神上给予支持。六四大屠杀后，世界各地齐声讨伐中共，谴责中共暴行，纷纷游行、示威、捐款。尤其是海外华侨，身处异乡，心系祖国，历来是中国民主革命的摇篮。他们的支持是中国民主革命不可缺少的一部分。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;然而，海外背景复杂，无论是反共的民运组织，还是一般支持中国民主事业的华人，并没有一个有效的组织；西方国家往往不得不优先考虑自己国家利益，而中共充分利用西方国家自由民主的社会制度进行欺骗宣传，收买个人或团体为其服务。因而接纳和筹集海外捐款是一项复杂的工作，特别是在革命准备阶段，海外筹款应高度警惕。<br />
&nbsp; &nbsp;<br />
<br />
第三节．党营事业<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;民主革命准备阶段，党营事业所得只能成为党的经费辅助来源，而不应将有限的精力投入经营。党营事业主要是在配合地下活动的同时，给革命政党提供一些经费和援助，并相机取得解决食宿、交通、通讯、掩护等功能。<br />
&nbsp; &nbsp;<br />
<br />
第四节．国内行动筹款<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;民主革命政党经费的主要来源应自始至终立足国内行动，其他来源应作为辅助手段。一个革命政党的经费需求将随其活动的展开而日益增大，任何外来援助都不能完全解决这种需求。同时，民主革命者必须永远牢记革命的实质和基本核心是权力和财产的重新分配。我们其所以要推翻中共，正是中共没有也不可能实施公平的权力和财产分配。因而，在革命准备阶段和进行阶段用革命手段实施权力和财产的再分配不仅是革命资金的重要来源，而且也是革命的目的之一。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;采取革命行动筹集资金时，必须始终将行动的矛头对准共党贪官污吏，尤其是那些民愤极大的贪官污吏。相反，靠自己勤劳致富者不仅不是我们行动的目标，而且是民主革命应当保护的对象。共党贪官动辄贪污数百万、数千万甚至上亿，其中牵涉不少人命和血债。因而，我们的行动是正义的，既筹集了资金，又鼓舞了普通民众反对共党的信心。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;我们必须具体谈谈绑架中共贪官的国内行动筹款。1987年，甘肃一副省长家中被盗70多万元现金。这位省长不但没有报案，而且行窃者被抓认罪后，此省长仍不承认被盗，因为他害怕交代那些贪污所得资金的来源。最近一个广东人冒充三陪女给全国各地官员写信敲诈，绝大部分官员都老实汇钱到他指定的账户。有些人不明白为什么中国现在“黑社会”那么猖狂，一些中共官员完全被控制。真正的原因是这类贪官的罪恶被那些江湖老大所掌握。我曾见过湖南一位“大老”当众羞辱一位公安局长，说“以前白天他是局长，晚上我是。现在白天黑夜我都是公安局长，他是狗屎！”。因而，绑架中共贪官，首先肯定能获得一笔资金；其次被绑的贪官绝大部分不敢声张，因为一则他怕丢官、判刑甚至枪毙，二则害怕报复；最后被绑贪官将为我所用，真所谓一举多得。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;民主革命者事先应对被绑贪官作些基本调查，尽管绝大部分中共官员都不同程度地贪污受贿，但有的放矢仍然是必要的。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;另外必须作好善后准备工作，一旦被绑贪官事后报案，或私下报复，革命者应有能力对其处置。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;其次的目标是国有财产。国有财产本是广大民众的共同财富，而今天正一步一步流入共党官僚的私人口袋。革命者有义务有责任将其夺回，用于民主革命和分配给广大贫苦民众。<br />
&nbsp; &nbsp;<br />
<br />
第五节．革命起义后的经费来源<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;一、没收共党官僚资本<br />
&nbsp; &nbsp;民主武装革命起义后，民主政党必须没收自由区内所有官僚资本，这既是党的经费来源，也是革命目的之一。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;二、接收国有资产<br />
&nbsp; &nbsp;国有资产包括银行、商店、共党党政军警司法系统以及其他一切所谓国家所有制企业的固定资产和流动资金。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;三、税收<br />
&nbsp; &nbsp;革命政党应在自由区建立公正的税收制度，作为革命经费的长期来源之一。税收政策首先要坚持公平的法则。同时最重要的是必须时刻服务于推翻中共、夺取政权这个大目标。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;四、党营资产<br />
&nbsp; &nbsp;民主革命应在自由区建立自己的党营资产，一方面提供党的经费，另一方面为推翻中共而服务。经营党产时应特别注意两个问题：一是不能让经营冲淡革命的目的，始终要围绕民主革命这个大目标；二是防止经营者脱离、甚至叛逃，尤其是掌管数量较大的资产者。因此，必须建立必要的监督政策。</div>

							
							
							
							
