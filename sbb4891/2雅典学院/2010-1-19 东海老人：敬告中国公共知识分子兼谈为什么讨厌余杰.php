																											<div id="postmessage_441658" class="t_msgfont">第十五章 武装革命中的对外关系<br />
<br />
<br />
&nbsp; &nbsp;尽管实现中国民主自由主要取决于中国国内民众的奋斗，但外部的援助却是不可缺乏的部分，尤其是在国际交流日益频繁、信息网络形成全球系统的今天，中国这样一个大国的民主革命必将对世界格局产生深远的影响。世界各国、各组织必然密切关注并且介入中国民主革命的进程。因而，民主革命领导人必须具备现代国际关系的知识、视野，正确地处理国际关系。<br />
<br />
第一节．西方民主国家的援助<br />
&nbsp; &nbsp;一、武装起义前的援助<br />
&nbsp; &nbsp;必须清醒地认识到，民主革命武装起义前不可能取得西方民主国家的大力援助。这是因为首先西方民主国家不至于象苏共输出共产革命那样支援民主革命。二十世纪下半世纪，东西方冷战时期，共产主义集团处处挑衅，民主阵营则依仗雄厚的资源与之周旋，达到不战而胜的目的，实践证明这是一条有效的战略。另外，民主国家的社会结构也决定其不可能采取大规模民主革命输出的政策。法国、美国在越南战争中的实践证明，西方国家人民对卷入战争的态度是消极的，与其说法美对越战争失败，不如说是两国人民倦战的结果。<br />
&nbsp; &nbsp;其次，正如我在第五章中指出，国家主义仍然并在将来很长一段时间继续是国际关系中最重要的原则。西方国家在与中共关系中，最优先考虑的仍然是其国家利益。我们可以经常从无数西方国家领导人演说中听到一个稳定的中国符合西方国家利益，这个利益指的是西方国家的国家利益，尤其是他们与中国贸易的利益。<br />
&nbsp; &nbsp;再次，西方国家对中国历史演变的法则仍然没有完全了解。无数政要和学者都无法理解为什么共产主义的始祖苏共垮台十几年，中国又实行半社会主义、半资本主义的混合体，且共产党普遍腐烂，而中共却还没有垮台。殊不知中国文化中从来不像西方国家那样注重原则，而总是抓住现实利益紧紧不放，因而，西方国家总等待一个中国的戈尔巴乔夫、叶利钦式的人物出现。但这种人物是很难出现的，即便出现，也起不了戈、叶那样的作用。中国政治舞台从来是以实力为依据的，西方国家的这种思维逻辑也决定他们对中国民主革命的态度。<br />
&nbsp; &nbsp;二、武装革命起义后的援助<br />
&nbsp; &nbsp;尽管西方国家从国家利益原则出发，在中国民主革命武装起义前不太可能支持民主革命，或者肯定不会公开明确支持，但这绝对不等于他们不会支持已经起义并站稳脚根的中国民主政党的革命。因为推翻中共这样一个邪恶的政权，从最根本的目标仍然符合西方国家的国家利益，又有利世界和平和人类文明历史的发展，特别是在西方国家人民的民主传统舆论的压力下，西方国家自然会本能地倾向中国民主政党。因此，民主革命政党在武装起义后应开展对西方国家的交往，争取他们的援助，而决不能象太平天国那样闭目塞听，孤军奋战。<br />
&nbsp; &nbsp;另一方面，我们仍要认识到即使武装起义相当一段时期之后，我们也不能奢望获得西方国家明确公开的支持。因为西方国家必须考虑到中国是一个拥有核武器的大国，又是联合国常任理事国。西方国家的支持必然要视中国民主革命发展的进程而定。民主革命越发展，西方的支持越明确、越公开。<br />
&nbsp; &nbsp;三、西方国家援助的内容<br />
&nbsp; &nbsp;1、 情报。<br />
&nbsp; &nbsp;准确及时的情报对民主政党是极为重要的。西方国家拥有先进有效的情报系统。卫星监视地球每个角度；电子侦测可以截获全球所有的信息。这种情报对于处在游击战、运动战时期的民军相当重要。另一类情报是国际关系，中共的动向，这对中国民主政党制定大方针很有帮助。<br />
&nbsp; &nbsp;2、 国际压力。<br />
&nbsp; &nbsp;西方国家能够公开且名正言顺地实施的政策是呼吁中共停止内战，实施多党民主政治，开放言论自由，这些正是中国民主政党的奋斗目的。<br />
&nbsp; &nbsp;3、 资金、技术和武器。<br />
&nbsp; &nbsp;这种支持肯定是秘密的。<br />
&nbsp; &nbsp;4、 人力支持。<br />
&nbsp; &nbsp;这包括华裔在西方的中国人及西方的志愿人员。民主革命政党急需大批有西方民主生活体验的干部。没有西方国家的支持，这种活动难以展开。<br />
&nbsp; &nbsp;总之，西方国家的支持是广泛的、形式多样的。民主政党武装起义后，应着手与西方国家建立秘密联系。交往中认清一个基本原则：无论从道义、理论、国情、最终利益出发，西方国家一定是中国民主运动坚强后盾，同时考虑到西方国家的国家利益和现实国际关系。<br />
<br />
第二节．海外华侨的支持<br />
&nbsp; &nbsp;一、华侨的概况<br />
&nbsp; &nbsp;中国有一个很特殊的地方：有将近四千万海外华侨，这些华侨主要分布在东南亚。尽管许多人已加入所在国国籍，有的居住数百年，但大部分华侨仍保留中国传统文化，会说中国话，并对中国的命运极为关心。1989年“六四”运动时，海外华侨的支持让全世界为之感动。孙中山先生的革命运动源于华侨，成长于华侨，几乎可以说没有海外华侨的支持，就没有孙中山先生的革命。<br />
&nbsp; &nbsp;自中共夺取政权以后，海外华侨经历了许多痛苦的精神折磨。一方面为西方殖民帝国离开中国而高兴，希望中国繁荣强大；另一方面又无法认同共产主义理论，憎恨中国共产党的暴行。海外华侨经过自己的勤奋努力，大多为当地富裕之家，尤其是东南亚的华侨，大都可以左右所在国的经济命脉。而共产党的革命首先要打倒富人，海外华侨就在这种民族主义和共产主义困扰中挣扎，最后不得不放弃中共的共产主义教条，这便是中共在东南亚共产革命失败的根源。<br />
&nbsp; &nbsp;中共开放以后，不再输出共产革命，不再打倒资本家，海外华侨的敌对情绪消失了许多，加之中共广泛招揽、笼络华侨，不少华侨回中国投资，中国经济发展与华侨的个人利益又息息相关。但中共的腐败却越来越使海外华憎恶，不少华侨逐渐认识到没有民主制度就无法保障资本主义。<br />
&nbsp; &nbsp;二、如何争取华侨支持<br />
&nbsp; &nbsp;1、建立民主革命政党海外组织。<br />
&nbsp; &nbsp;2、宣传民主自由思想。这种宣传应特别注意说明中共不等于中国，反中共不等于反中国。中国的强大只能在推翻中共后才能实现。不少华侨为中共所用，除一些人与中国有生意往来而顾及自己利益以外，许多人是被中共虚伪的爱国主义所迷惑。<br />
&nbsp; &nbsp;3、利用华侨与中共的联系，发展地下活动。许多华侨在中国投资，他们与中共官员有千丝万缕的联系，有的还与中共高层官员关系密切，有一些有利条件收集情报。民主政党应充分利用这一有利条件。<br />
&nbsp; &nbsp;4、争取人力和资金援助。华侨生活在资本主义社会，本能拥护西方民主制度。又有民主生活的经历，应当是民主政党的干部来源之一，尤其是经济、外交人才。另外，华侨也能提供相当的资金援助。<br />
<br />
第三节．海外民运的作用<br />
&nbsp; &nbsp;一、海外民运简介<br />
&nbsp; &nbsp;自1983年中国民主团结联盟在美国成立以来，中国民主运动持续在海外活动了二十年，成功地高举中国民主运动的旗帜，极大地鼓舞了国内民主斗士反抗中共暴政的精神，并团结了一批立志中国民主事业的骨干分子。<br />
&nbsp; &nbsp;海外民运主要分三个时期，第一时期是从1983年到1989年，主要成员是海外留学的知识分子；第二个时期是1989年到1998年，这个时期主要是六四流亡人物，有学生、知识分子、工人及前中共官员；第三个时期是1998年中国民主党建立到现在，主要人物是六四后坚持在中国从事民主运动而被中共流放者。海外民运的组成主要还是以知识分子为主。<br />
&nbsp; &nbsp;海外民运并没有形成统一的组织，造成这种现象的原因是多方面的，然而最重要的是长期生存在西方国家、远离中国大陆、缺乏基本的群众基础，形成有政党无活动、有组织无纪律、有领袖无成员的局面。另外各种势力介入民运组织之中，培养、支持自己的力量也是民运分裂的主要原因。此外，民运人士的思想除实现中国民主自由外，许多理论思想不能统一，因而形成不了一致的观念，以至于无法达成统一行动。最后民运人士本身也在积累政治活动经验，完善自身素质。<br />
&nbsp; &nbsp;西方各国对海外民运的态度也是根据国际环境变化的。总体来讲，西方国家是支持民主事业的，特别是中国的基本人权。六四时期，西方政府和民众对中国民主事业的支持达到高潮，但因无法推翻中共，这种支持逐渐冷却。<br />
&nbsp; &nbsp;海外民运有一些有限的资金，只能让少部分人士不打工而从事专职民运活动。这些资金来源包括西方国家政府的民运活动基金；民间社会的赞助；华侨组织和个人的赞助；台湾政府和民间的支援；香港和澳门组织和个人的支援。这些援助既有限，也没有保障，而且有一定局限性。<br />
&nbsp; &nbsp;二、民主革命与海外民运的关系<br />
&nbsp; &nbsp;海外民运一些组织和个人或出于自身经历观点、或出于背后援助组织的压力，不支持以革命方式推翻中共，甚至坚决反对这一途径。这是一种正常现象。一切政治运动总有左、中、右派，而这些组织和个人会随中国和世界局势的变化而改变自己的思想。因而中国民主革命要紧紧依靠海外民运中的革命派，让中国民主革命国际化。<br />
&nbsp; &nbsp;必须确定，海外民运只是中国民运的一部分，中国民运的主体和领导层应始终在中国国内，任何遥控指挥式领导一定会失败。革命领袖必须而且只能是在中国国内斗争中产生。海外民运人士只能回到中国，参与中国民主革命，才能逐步驾驭中国民主革命的洪流。自封式的领袖只是一种幼稚的政治幻想。<br />
&nbsp; &nbsp;三、海外民运的作用<br />
&nbsp; &nbsp;1、 联络西方政府和民间组织，争取他们对中国民主革命的支持。<br />
&nbsp; &nbsp;2、 宣传中国民主革命，以争取西方政府、组织、个人和海外华侨的认同。<br />
&nbsp; &nbsp;3、 向国内输送民主革命骨干力量，尤其是受过西方教育的青年知识分子。<br />
&nbsp; &nbsp;4、 收集情报信息。<br />
&nbsp; &nbsp;5、 建立海外民主革命联络点。<br />
&nbsp; &nbsp;6、 筹集资金。<br />
&nbsp; &nbsp;7、 建立进入中国秘密通道。<br />
<br />
第四节．台、港、澳：中国民主的基地<br />
&nbsp; &nbsp;一、台湾：中国民主的榜样<br />
&nbsp; &nbsp;台湾是中国人独立建立的第一个民主政治实体。自孙中山先生倡导三民主义，主张军政、训政、宪政三阶段民主进程以来，国民党在台湾既成功地继承、保留、发展了中国几千年优秀的文化，又成功地吸收西方文化的精髓，在中共威胁、封锁下不但发展了现代化经济，而且逐步实现多党政治、言论自由的政治格局。在中共独裁暴政的几十年中，台湾始终犹如黑夜茫茫大海中的灯塔，照亮了中国人的航程，鼓舞着中国人民反抗中共的残暴专政。中共历来吹嘘他们胜利了，国民党失败了，但我们认为中共只不过暂时在大陆成功地篡夺了政权，而国民党的国民革命必将取得最后胜利，自由民主的政治体制的种子必将穿越台湾海峡，在大陆生根开花，繁荣茂盛。从这个意义上来说，中国民主革命可以说是再次国民革命。无论从何角度来看，台湾应始终是中国民主革命的坚强后盾，甚至可以成为有力的领导者。<br />
<br />
&nbsp; &nbsp;然而，台湾的现实政治对台湾在中国民主革命进程中的作用有着相当的局限性。民进党倡导的台独在相当程度上削弱了台湾对大陆的影响，使中共在很大程度上转移了大陆人民对中共暴政不满的注意力，而成功地以爱国主义大旗鼓动大陆人民反对台湾，如同斯大林利用纳粹的侵略加紧对内的镇压一样，中共也在利用“分裂势力”维系其日益衰落的统治。国民党逐渐失去了当年蒋介石先生、蒋经国先生反攻大陆的信心，只是为争取在台湾重新执政而挣扎。亲民党的注意力当然只是台湾。因而，台湾执政、在野政党似乎都缺乏在中国实现民主自由政治的思想基础和信心，而一味只关心台湾自己的事务，而忽略一个基本的现实：台湾不可能孤立于大陆事务之外。<br />
&nbsp; &nbsp;在这种情况下，中国民主革命要取得台湾的支持便有一些困难和局限性。尤其是革命前期，因而民主革命政党应积极争取改变台湾执政、在野政党的态度。<br />
&nbsp; &nbsp;首先是认清中共表面强大的虚假性，中国历史上的皇朝和东欧、苏共独裁暴政都是在虚假强大下被推翻的，其次是将政党和个人的视野扩展在十三亿人口，近一千万平方公里的中国大陆，抛开偏安东南孤岛的狭隘政治心态。我们相信台湾仍有许多这种抱负的组织和个人。如台湾能积极而勇敢地参与中国民主革命进程，台湾将起着举足轻重的作用。<br />
&nbsp; &nbsp;我们其次的希望当是争取台湾的支援，无论台湾执政、在野政党的政治诉求如何，推翻中共独裁暴政应该是所有组织和个人的愿望，中共一日不被推翻，台湾一日不得安宁。中国民主政党自始至终应把获得台湾官方、民间的支持作为自己的重要任务。台湾的各政党肯定是中国民主化后多党政体中的重要成员，台湾的多党制，尤其是从训政到宪政的转变过程，将是中国民主的宝贵经验。<br />
&nbsp; &nbsp;二、港、澳的地位<br />
&nbsp; &nbsp;尽管中共已收回港、澳法律上的主权，并逐步加紧对港、澳的控制，但中共不可能象49年控制上海、广州那样将民主社会的精华消灭殆尽。港、澳资本主义社会制度和由此而生的民主社会结构存在了近百年，自由民主的生活已深入每个市民的生活之中。<br />
&nbsp; &nbsp;中共承诺香港社会制度五十年不变，这个承诺一半是真的，一半是假的。真的部分是香港的市场经济制度不会变，事实上中共试图改变自己的经济体制而与香港靠拢；假的部分是中共不会让香港自由民主政治制度长久存在，而是逐步钳制香港言论自由和民主选举，将香港纳入中共一党独裁的政治体系。中共一天也没有停止加紧这种控制。最近的23条便是这种企图的集中表现，一旦反对之声慢慢平静，中共会采取下一个更严厉的措施，直到完成其一党独裁，听不到任何反对声为止。港、澳民众应始终清醒地认识到，中共决不会让港、澳民主自由社会制度存在，进而影响大陆，成为中国民主的桥头堡。<br />
&nbsp; &nbsp;中国民主革命政党应针锋相对，充分利用港、澳目前的社会环境，使之成为未来中国民主事业的基地。首先要利用港、澳目前的言论自由和国际社会的舆论，让所有港、澳民众认清中共独裁政权的本质，理解中共政权与港、澳社会生活是根本无法长期并存，不是中共独裁政权的垮台，便是中共在港、澳实现彻底独裁化。其次要让民众屏弃逃避、侥幸的心理，只管自己的经营，不问政治前途，认识到在腐败、独裁中共的控制下，港、澳大大小小的资本家是没有保障的，大陆内部的个体资本家的遭遇便是活生生的例子。从这点看，台湾拒绝中共的所谓“一国两制”是正确的，这是国民党与中共几十年斗争的经验。<br />
&nbsp; &nbsp;中国民主革命政党应在港、澳建立自己的秘密活动的根基。尽管中共几十年不断在香港进行渗透，尤其在97以后，但香港的社会活动空间仍然存在。民主革命政党首先应与坚持香港民主政体的民主党保持联系，充分发挥他们的合法性作用；其次利用港、澳与大陆的联系，建立秘密通道，策反中共各类官员，收集各种情报，与外界保持联系，筹集资金、武器。总之，港、澳虽不是军事战场，但却是与外界联系的中心，地下活动的焦点。<br />
<br />
第五节．与其他反共组织的关系<br />
&nbsp; &nbsp;除中国民主革命政党以外，国内外还有许多其他反对中共或中共视其为敌对组织的团体。如何处理与他们的关系，应当是民主革命政党的任务之一。一个总的原则便是：联合一切可以联合的力量，集中一切可以集中的力量推翻中共。因此，所有反对中共的、或中共视为敌对的组织都是中国民主革命的朋友，至少不是敌人。我们不但要努力获得这些朋友的支持，消除彼此之间目标不同的误解，更重要的是防范中共的分化瓦解、挑拨离间，自始至终将革命的矛头对准中共。<br />
&nbsp; &nbsp;一、宗教团体<br />
&nbsp; &nbsp;中国有佛教、基督教、伊斯兰教、道教等宗教团体。他们的目标是宗教信仰自由，这正是我们推翻中共实现中国民主的目的之一。中共几十年来不断残酷镇压各种宗教信仰者，成千上万信徒为他们的信仰自由而殉道。单中共仅在镇压宗教信徒上的罪行便罄竹难书。中国各类信徒有数亿人，因而，宗教团体是我们坚决的盟友。<br />
&nbsp; &nbsp;但是，中国各种宗教信徒大多有一个共同的特点：和平地、或者消极地反抗中共，极少主张武装革命，一方面这是由于信仰的局限性，另一方面是缺乏武装推翻中共的勇气和信心。因而，民主革命政党在处理宗教团体关系上应坚持以下几个原则：<br />
&nbsp; &nbsp;1、 宗教团体是我们的坚决的盟友。<br />
&nbsp; &nbsp;2、 鼓动所有信徒武装反对中共。<br />
&nbsp; &nbsp;3、 不幻想所有信徒参与或支持武装革命。<br />
&nbsp; &nbsp;必须指出的一个问题，尽管大多宗教团体采取和平、消极的方法追求自由的信仰，但古今中外仍有不少宗教团体以武装革命的方式实现自己的信仰。基督教十字军东征，伊斯兰教的圣战都是突出的例子。中国民主革命政党必须容纳以武装革命反对中共的宗教团体。<br />
&nbsp; &nbsp;二、黑社会组织<br />
&nbsp; &nbsp;黑社会组织是以暴力为手段，以金钱为目的半公开的集团。中国目前大大小小的黑社会多如牛毛，对中国社会的影响越来越深。正确认识黑社会组织的性质，妥善处理与黑社会组织的关系是中国民主革命政党的一大重要任务。<br />
&nbsp; &nbsp;首先必须认清黑社会的形式和发展是政治腐败、道德沦丧、法制崩溃的产物。黑社会组织与普通刑事犯罪的最大区别不是使用暴力、或以暴力相威胁，而是黑社会组织获得政府官员的庇护甚至参与，半公开、半合法地较长时间地存在。中共的普遍腐败，法律形同虚设为黑社会组织的存在和发展提供了基础，而且可以肯定，黑社会组织将会不断发展壮大。<br />
&nbsp; &nbsp;其次，要了解黑社会组织是对中共腐败政权本能的反抗和对自我的自然保护。中国大小官僚可以用手中的权力榨取各种利益，渔肉百姓，而普通百姓既无生活保障，又无公平司法保护，于是一些聪明而勇敢者便集成一团，联络官员，坐大一方，既可获得经济利益，又能显示自己的威力。因为中共毫无公平司法体系，一些大大小小的私人企业家无法从官方获得公正的司法保护，而且经常受工商、税务、市容、公、检、法等官员的敲诈，不得不寻求黑社会组织的保护，或者参加黑社会。一些政府官员指示自己的黑社会组织敲诈私人企业家，甚至打击、暗杀自己的政敌。参与黑社会组织的成员绝大多数遭遇到中共不公正的司法迫害，有的沉冤多年而无处昭雪，有的在狱中受尽欺凌，因此转而组织、参与自己的组织，既保护自己，又发泄内心的愤怒。<br />
&nbsp; &nbsp;最后，我们应认清黑社会组织无明确的政治目的的特性。这种特性使他们经常与中共官僚一起欺压普通百姓，敲诈弱小百姓，甚至迫害、残杀无辜百姓。<br />
&nbsp; &nbsp;了解了黑社会组织的形成、性质后，我们在对黑社会组织问题上应掌握以下几个原则：<br />
&nbsp; &nbsp;1、争取黑社会组织参与中国民主革命。由于黑社会组织是本能反抗中共的集团，这种团体自然是中国民主革命联合的对象。事实上，中国历史上革命造反者与黑社会组织总有千丝万缕的联系，许多造反者本身就是黑社会组织成员。而且，一些无政治目标的黑社会反抗组织最后总是慢慢发展成政治集团。李自成、张献忠最初纯粹是打家劫舍的团伙，最后逐步走上夺取政权的道路。因而，中国民主革命政党绝不可机械地、抽象地、片面地看待黑社会组织，应主动引导他们走上反抗中共的道路，一旦成功，黑社会组织的功能便能发挥很大的作用。<br />
&nbsp; &nbsp;2、 中策是与黑社会组织合作。这种合作当然不是参与其以经济为目的的暴力活动，而是通过其集团的力量，实现我们的政治目的。例如，控制某中共官员，建立地下活动据点，收集各种情报等等。<br />
&nbsp; &nbsp;3、 劝阻谴责黑社会组织针对普通百姓的暴力活动。中国民主革命政党的最终目标是建立一个民主自由的中国，给普通百姓一个公正、健全、合理的社会制度。任何对普通百姓的暴力必然与革命的目标背道而驰，因而必须坚决制止，尤其是在革命政党控制的自由区。<br />
&nbsp; &nbsp;三、法轮功、中功<br />
&nbsp; &nbsp;法轮功、中功等组织既不象传统的宗教组织，有悠久的历史、广泛的经典、系统的组织，当然更不象黑社会组织那样以暴力为其活动的基础。不管这些集团的信仰本身是否经得起严肃的哲学、历史考证，也不管其是否有利于中国民主事业，但他们的信仰自由本身却是中国民主革命政党要追求的目标之一，仅仅从这一点来说，我们必须支持法轮功、中功的诉求。<br />
&nbsp; &nbsp;法轮功、中功的纯粹的信仰追求使之吸引了不少信徒，参与经济也使他们有一定的经济实力，尤其是中共对他们迫害，使他们成为一个世界性的组织，反而促进他们的发展。成为中国社会一支值得重视的力量。<br />
&nbsp; &nbsp;但我们必须清醒地认识到这些团体的局限性。他们一直游离在宗教和政治团体之间，既不承认自己是政治团体，又不认为自己是宗教团体，这对他们的发展是不利的。无论是政治团体或宗教团体，我们必须让其明确中共是决不会让他们有信仰自由的，必然倾全力打击、迫害，事实上成千上万的法轮功信徒被中共逮捕、关押、拷打、判刑，有的被折磨致死，而且海外的法轮功信徒无数次抗议中共的暴政，从而法轮功实际上在与我们从事一个共同的事业：为信仰自由而奋斗。<br />
&nbsp; &nbsp;法轮功、中功等团体可能不会支持中国民主革命政党的武装革命，但这并不影响他们成为我们的盟友，实现中国民主事业需要各种不同形式、不同渠道的努力。<br />
<br />
结 语<br />
&nbsp; &nbsp;这不是一本理论书，因而没有按学术的要求指出研究的资料，考证出处，在此，我对所引资料的作者表示感谢。书中的内容是本人的经历和二十多年思考、研究的产物。我希望首先让人们认清推翻中共的必要性；其次是建立人们推翻中共的信念，不可被中共这个纸老虎所吓倒；然后，论述为什么和平改良行不通；最后是这本小册子的主要内容：革命的理论和方法。<br />
&nbsp;&nbsp;必须指出的有三点：<br />
&nbsp; &nbsp;尽管我强调中国民主革命武装起义是我们的重点，但我们绝对不排除、不拒绝中国的和平变革，而且当这种变革来临时，应竭力抓住时机，促使中国的和平转变，即便是在革命过程中，也不能放弃促成这种和平转变。<br />
&nbsp; &nbsp;另外必须指出的是，革命武装起义是一门实践科学，本小册只是论述一些基本的问题，大量的实践必须由民主革命者根据具体情况采取相应的策略。革命者必须牢牢记住：若在中国政治舞台上成功，必须通晓中国历史；若希望成为现代中国领导，必须要有国际视野。<br />
&nbsp; &nbsp;最后我坚信时代在呼唤英雄，正所谓“江山待招才人出，各领风骚数百年！”时代出英雄，这个时代正等待许多英雄人物的出现，他们必将乘民主自由之风浪冲垮中共暴政，并重振中华文明！</div>

							
							
							
							
