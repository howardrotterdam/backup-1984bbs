																											<div id="postmessage_422253" class="t_msgfont">周恩来也是个大卖国贼 <br />
　　 <br />
　　 <br />
　　<br />
　　由邓小平归结的中共“三核心”，毛泽东、邓小平、江泽民都是卖国贼。毛泽东卖国，邓小平卖国，江泽民卖国。如今的胡锦涛也是个不折不扣的卖国贼。总之，中共的核心都卖国。中共党就是一个卖国党，是一个帮助俄国毁华灭华的党。毛核心时期的周恩来，同样是个大卖国贼。周恩来与毛泽东合夥卖国。毛泽东的卖国行径都是经周恩来的手实现的。 <br />
　　<br />
　　1949年底，在中共政权成立伊始，为了取得苏俄对他们的支持，毛泽东不顾冰天雪地，冒着严寒，坐着专列，千里迢迢奔向莫斯科。 <br />
　　<br />
　　在莫斯科，斯大林为了不让毛泽东搞民族主义，不做铁托第二，曾经关了毛泽东的“禁闭”；曾经开大会批斗毛泽东。在毛泽东被“禁闭”期间，毛泽东整天不得出门，不能见斯大林，也不能见其他人。按毛泽东自己的话说，他在莫斯科的任务就是吃饭，睡觉，拉屎。在斯大林开大会批斗毛泽东时，毛泽东被吓得屁滚尿流，不敢吭一声，连个屁都不敢放。用毛泽东自己的话说，祗是“在鼻子里笑他们”。好不阿 Q 。 <br />
　　<br />
　　在中国现代史的降敌人员中，还没有那一位被敌方批斗过。溥仪做满洲国的皇帝，事先日本人没有斗过他。汪精卫投靠日本，在南京登基，事先日本人也没有批斗他。祗有这位“伟大领袖”毛泽东让俄国人批斗得像个三孙子。好不悲哀。这也是他对马列主义的创造和发展吧。 <br />
　　<br />
　　在斯大林的咄咄逼迫下，毛泽东向斯大林举手全面投降，乖乖地承认斯大林的所有侵华要求。在这种情况下，毛泽东于 1950 年一月二日急电周恩来迅速赴苏。 <br />
　　<br />
　　周恩来慌慌张张，连准备带赶路，于 1950 年一月二十日，匆忙赶到莫斯科。 <br />
　　<br />
　　自 1950 年一月二十二日至二月十四，在莫斯科，仅祗二十几天的时间里，由周恩来亲手签订了六十六项丧权辱国，出卖中国领土和主权的条约、协定、议定书和补充协定，全面接受了苏联的侵华要求。周恩来卖国的质量和数量，那可不是李鸿章能够比得上的。 <br />
　　<br />
　　周恩来在二十几天里签订的条约、协定等文书目录如下： <br />
　　<br />
　　中苏联合公报；<br />
　　中苏友好同盟互助条约及《补充协定》；<br />
　　关于重申和保证蒙古人民共和国独立地位的换文；<br />
　　中苏关于中国长春铁路、旅顺口及大连的协定及《议定书》；<br />
　　关于苏联贷款给中华人民共和国的协定及《议定书》；<br />
　　关于 1945 年 8 月 14 日中苏签定的（与中华民国国民政府签定的）相应条约和协定失效的换文；<br />
　　关于苏联把它在东北接收的日伪工商业及财产、原俄国在北京的兵营移交给中华人民共和国政府的换文；<br />
　　中苏建立电报电话联络协定；<br />
　　中苏互相交换邮件和包裹的协定；<br />
　　中苏创办四项合营公司的协定；<br />
　　50 份“经济合作项目”。 <br />
　　<br />
　　以上合计共 66 项。其中《补充协定》及两份《议定书》都是绝密的，也就是见不得人的，是偷偷的卖国文件。 <br />
　　<br />
　　由上可见，周恩来签订的卖国契约的数量，大大的，远远的超过了李鸿章。李鸿章仅祗是与俄国签定一份“中俄密约”。致于周恩来签定卖国文件的质量，那可是李鸿章绝对望尘莫及了！李鸿章仅祗是出卖一个筑路权，有一个把台弯割让给日本的出卖领土账。 <br />
　　<br />
　　签订的这些卖国契约时的周恩来是甚麽情况？ <br />
　　<br />
　　无论是周恩来，或是毛泽东，他们在此之前都是用尽全身的解数，替苏联反对中国政府，必欲打倒之而後快。他们对中国的地理、地质、矿藏、中国经济，等等方面完全是白痴，丝毫不懂。而在他们霸占的山沟里没有一位地质专家，也没有一位经济学家。 <br />
　　<br />
　　而苏俄方面却是对中国虎视眈眈，偷窥侦查，积累二百多年的侵华经验。他们有专门研究中国的机构，对中国的地理、矿藏、经济等状况，了如指掌。所以他们能够提出各个方面侵略中国的要求。这六十多项契约是苏俄老早就准备好了的。 <br />
　　<br />
　　周恩来在根本不了解中国国情的情况下，签订这些契约；完全是苏俄要甚麽，周恩来就答应甚麽，给甚麽；对中国的领土、财富、主权，敞开大门大出卖。 <br />
　　<br />
　　周恩来都出卖了甚麽？ <br />
　　<br />
　　从以上周恩来签订的一系列卖国账单，就可看出，周恩来把中国的领土、领空、地上、地下，凡是能够出卖的，他都给出卖了。这里暂不详细诉说周恩来的卖国细账，仅就出卖中国领土和主权的问题说说。 <br />
　　<br />
　　周恩来出卖了中国神圣领土外蒙古。 <br />
　　<br />
　　外蒙古土地自古以来就是中国神圣领土不可分割的一部分。蒙古族是中华民族重要的组成部分。蒙古族在中华大地建立的元朝延续一百多年，是中华民族历史不可或缺的朝代。蒙族人民拓展了中华民族的疆域，传播了中华文化。就是这样的宝地，这样优秀的民族，竟被周恩来给出卖了。周恩来签订“重申和保证蒙古人民共和国独立地位的换文”，就是周恩来出卖外蒙古的铁证。 <br />
　　<br />
　　周恩来偷偷出卖了唐努乌梁海。唐努乌梁海在外蒙古的北边。土地面积相当于台湾的4.7 倍。这样一块大的土地，周恩来不声不响的就把它给出卖了。<br />
　　周恩来出卖了新疆和中国的东北。他签订的“中苏友好同盟互助条约”的附件《补充协定》，就是他出卖新疆和中国东北给苏俄的铁证。把新疆和东北划为“苏俄的势力范围和殖民地”。周恩来亲自划押签字，证据握在人家手里。想赖也赖不掉。 <br />
　　<br />
　　以上，经周恩来之手出卖的中国领土计：<br />
　　外蒙古 154 万平方公里；<br />
　　唐努乌梁海　17 万平方公里；<br />
　　新疆　　　 160 万平方公里；<br />
　　中国东北　 100 万平方公里。<br />
　　合计共　　　　　 431 万平方公里。 <br />
　　<br />
　　这 431 万平方公里，相当于近半个中国的领土，也相当于 120 个台湾的面积，全都被周恩来给出卖了。 <br />
　　<br />
　　或许有人会说：新疆和东北不都还是划在中国的版图之内吗？是的，是划在中国的版图之内。但是划在中国的版图之内，不等于没有出卖，祗是现在不能让中国人知道他们把这麽大的地方都出卖罢了。一旦中国人都知道他们这样卖国，他们岂不就混不下去了！他们的“伟光正”岂不就破产了！他们岂不再也骗不下去了吗！ <br />
　　<br />
　　同胞们，不要着急，倘若中共继续霸占大陆政权，俄国帝国主义者迟早会“进住”新疆和中国东北的。一旦时机成熟，他们就会进来。君不见江泽民把大陆边防军後撤 500 公里吗？君不见江泽民不敢把他与俄国划的边界线公之于众吗？君不见江泽民故意掩盖和篡改俄国侵华史，把俄国打扮成是中国“历史传统，友好邻邦”吗？君不见江泽民向俄国人保证：“不许中国人提出领土要求”吗？，君不见胡锦涛拿大陆老百姓的钱，“请”俄国的飞机、大炮、军舰来中国的领土、领海、领空来示威吗？这都是这帮卖国贼给俄国侵略者进住中国热身啊！在今後，当这帮卖国者都死光，俄国侵略者就会依据中共的卖国条约进住中国。人们信也不信，那就走着瞧吧！不过，到那个时候，你已经做了正式的亡国奴，想反抗都已经晚了！现在对江泽民、胡锦涛那样公开卖国都不知反，不敢反，等成了真正的亡国奴之後，还可能反吗？现在的中国青年有可能一个都躲不过亡国奴的命运！　 <br />
　　<br />
　　周恩来的卖国仅祗出卖中国领土这一项，就罪大恶极，罪孽深重。前无古人，後无来者。他给中华民族，给中华子孙後代留下无穷後患。 <br />
　　<br />
　　毛泽东、周恩来在中华大地搞的共产主义革命，就是带着中共广大党员，走向投降苏俄帝国主义；就是带着中共党员去卖国。 <br />
　　<br />
　　二十世纪，祸害人类的两大主义，就是法西斯主义和共产主义。 <br />
　　<br />
　　共产主义是甚麽？苏俄的共产主义，就是帝国主义，扩张主义。中共的共产主义，就是投降主义，卖国主义。马克思主义是他们的外套，是鸦片烟。<br />
　　在二十世纪，日本是用明火执杖，真刀真枪侵略中国。苏俄是用思想侵略、政治侵略，侵略中国；是用马克思主义鸦片烟毒害中国，麻醉中国；是用在中国的代理人征服中国。日本人的真刀真枪没能征服中国。苏俄的马克斯主义鸦片烟却征服了中国。周恩来、毛泽东就是背叛中国，投降苏俄，贩卖苏俄马克思主义鸦片烟，来帮助苏俄麻痹中国人民，替苏俄祸害中华，毁灭中华的罪魁祸首。 <br />
　　<br />
　　周恩来、毛泽东在中国夺取政权後，他们之所以卖国，就是用卖国换取俄国人封他们为皇帝，做宰相。中共公开承认，他们是儿子党，是儿皇帝。<br />
　　他们做俄国的傀儡，远比溥仪、汪精卫要卑鄙千万倍。他们的罪恶也远比溥仪和汪精卫大千万倍。 <br />
　　<br />
　　评判中共的任何问题，都必须看到中共是个卖国党，是听命于苏俄，是为俄国服务的。中共在中国境内打“内战”，那不是内战，那是替俄国人打中国人，是替俄国侵略中国。如果把中共攻打政府，看成纯是打“内战”，那就太天真了！ <br />
　　<br />
　　苏俄是个不折不扣的帝国主义者。日本侵占中国东北，苏俄侵占中国外蒙古。苏俄用承认满洲国，换取日本承认蒙古国。苏俄用承认日本占领东蒙，换取日本承认苏俄占领的西蒙。日本人占领东北是侵略，难道俄国人占领外蒙古就不是侵略吗？日本侵华是帝国主义，难道苏俄侵华就不是帝国主义吗？狼崽胡锦涛能够把这个问题交代清楚吗？ <br />
　　<br />
　　难道毛泽东、周恩来因为信仰马克思主义，就有权任意杀人，有权卖国吗？他们究竟是那个国家的人？ <br />
　　<br />
　　中国人民在揭露和批判中共罪恶时，必须揭露和批判中共的卖国罪行。因为这是关系中华民族前途和命运的大事。 <br />
　　<br />
　　中共党员应该猛醒了。中共党不仅是个邪党、魔教，而且还是一个贼党、卖国党。中共党员入了这个党，就是上了贼船，就是帮助这个贼党卖国。中共党员如果还有爱国心，还爱中华民族，还爱自己的父母，那就趁早从这个贼船上跳下来。 <br />
　　<br />
　　海外广大侨胞如果热爱中华民族，热爱中华大地，那就千万不要再给中共吹喇叭，抬轿子。你给中共吹喇叭抬轿子，就是鼓励中共去卖国，是对中华民族的犯罪。 <br />
　　<br />
　　周恩来究竟是个甚麽人？ <br />
　　<br />
　　有人说，他当面是人，背後是鬼。这个话说得不错，但是还不够。从政治层面说，周恩来是个叛国投敌份子，是被俄国人收卖的奸细，是个大汉奸，是俄国人的孝子贤孙，是替俄国人祸我中华，毁我中华，灭我中华，卖我中华的罪犯。苏联斯大林死时，他如丧考妣，亲自跑到莫斯科去给斯大林吊孝。<br />
　　周恩来的闹革命，是为国家，为人民吗？如果是为国家，为人民，那他去卖国干甚麽？去给俄国人卖命干甚麽？周恩来有甚麽人格吗？国家、民族都可以出卖，那还有甚麽人格？周恩来这个人纯粹是个丧心病狂，没有人性的“特殊材料”的卑微小人。 <br />
　　<br />
　　周恩来的丑陋和卑鄙，阴险和毒辣，那是秦桧绝对比不上的。 <br />
　　<br />
　　给周恩来盖棺论定，他绝对是个大汉奸，大卖国贼。 <br />
　　<br />
　　将来，中共垮台後，当政权回到人民手里，人民给卖国贼塑像时，肯定少不了周恩来的位置。 <br />
<br />
周恩来放弃日本战争赔偿的三条理由 <br />
<br />
在田中访华之前，他曾表示，如果对方提出赔偿，只要数额适当，他打算赔！ <br />
<br />
<br />
1972年7月以后，中国为实现与日本邦交正常化进行积极的准备，周恩来总理就放弃战争赔偿问题作了下述指示： <br />
<br />
<br />
第一，中日邦交恢复以前，台湾的蒋介石已经先于我们放弃了赔偿要求，共产党的肚量不能比蒋介石还小。 <br />
<br />
<br />
第二，日本为了与我国恢复邦交，必须与台湾断交。中央关于日本与台湾的关系，在赔偿问题上采取宽容态度，有利于使日本靠近我们。 <br />
<br />
<br />
第三，如果要求日本对华赔偿，其负担最终将落在广大日本人民头上，这样，为了支付对中国的赔偿，他们将长期被迫过着艰难的生活。这不符合中央提出的与日本人民友好下去的愿望。&nbsp; &nbsp;<br />
<br />
<br />
<br />
<br />
周恩来的这番话其实说的很**，台湾放弃了赔偿难道就成为共产党也放弃赔偿的理由了吗？在共产党眼里，国共在别人眼里的肚量难道比整个中华民族的利益还要重吗？共产党难道要和国民党比谁更卖国吗？就算你说的对，国民党放弃日本赔偿，难道别人卖国的行为就成为你也卖国的理由吗？放弃战争赔偿能够促使日本和中国建交，整个民族的利益竟然成了共产党争取日本的筹码。最后一句话说的是最无耻的，话应该改一下，这么说“如果放弃日本对华赔偿，其负担最终将落在广大中国人民头上，这样，为了承担战争的创伤，中国人民将长期被迫过着艰难的生活。这不符合中央提出的解放人民让人民过上好的生活的愿望”，这样才合理，然而我们的总理竟然把卖国的话说得如此的慷慨激昂。&nbsp; &nbsp;<br />
<br />
<br />
一个国家的总理这样说，还有比总理更高的主席还曾经为了日本对中国的侵略表示感谢：没有你们皇军侵略大半个中国，中国人民就不能团结起来对付蒋介石，中国共产党就夺取不了政权。所以，日本皇军是我们中国共产党人的好教员，也可以说是大恩人、大救星。一个国家的领导人都争着比卖国、比无耻，日本人当然瞧不起我们中国人了，所以他们才敢公然的祭拜靖国神社，才敢否定南京大屠杀，否定日本侵华。日本右翼分子称南京大屠杀是二十世纪最大的谎言，其实他们否定南京大屠杀才是二十世纪最大的谎言，而中共就是他们的圆谎者。&nbsp; &nbsp;<br />
<br />
<br />
国民革命军坚持了十四年的抗战，和日寇进行了二十二次上百万人规模的大会战，小型战斗两万九千八百多次，陆军阵亡三百多万，海军空军几乎全军覆没，二百多位将军为国捐躯，然而没有人去纪念。是他们用鲜血换来了抗战的伟大胜利，是他们用鲜血换来了不平等条约的废除，是他们用鲜血换来了我们不被日寇所奴役，是他们用鲜血换来了我国成为联合国的创始国之一的伟大荣耀，然而在我们中国大陆，在他们为之献身为之抛头颅洒热血的土地上却没有一个像样的纪念碑，祖先的功绩被歪曲，被掩盖，这又是谁的过错呢？一个忘记民族的光辉历史的民族将失去自信，一个忘记民族的屈辱历史的民族将难免再次的承受屈辱！&nbsp; &nbsp;<br />
<br />
<br />
中共对南京大屠杀的纪念绝不是因为他们会站在正义的人民一边，和人民一起来谴责日本的罪恶行径，而不过是想转移人民的注意力，煽动人民对日本的仇恨，从而不去注意它曾经做过的那些罪恶的事情。它怕人民明白真相，明白它们在抗战期间是如何在延安种鸦片然后卖给我国人民、毒害我国人民，明白它们是如何的和日寇勾结把国军的军事情报送给日寇，明白它们是如何的消极抗战扩充势力，明白它们是如何的帮助日寇吃掉抗日的国民革命军等等罪恶的行径，它们害怕人民知道真相，所以在歪曲掩盖之余便借用对南京大屠杀的纪念而转移分散人民的注意力。所以它们才不会去纪念那些为国捐躯的数百万英烈，所以才会在纪念南京大屠杀的时候只是做些表面文章，而不能使日本当局真正的做到对中国人民谢罪，更正他们的历史教科书，给予我国受害同胞应有的战争赔偿。&nbsp; &nbsp;<br />
<br />
<br />
我们纪念过去的事情，目地不是只是为了记住一件事情，而是希望能够从这件事情中得到启示和教训，而使我们民族能够不重蹈覆辙，然而，我们没有做到，悲剧仍然不断的发生。我不是反对纪念南京大屠杀，可是我们总得通过纪念能够反思能够清醒，否则我们民族将不能拒绝悲剧的一再发生，那这样的纪念还有什么意义呢？&nbsp; &nbsp;<br />
<br />
<br />
即使是针对南京大屠杀的纪念，也根本没有获得任何有用的收获。中共为了和日本建交，出卖了国家和民族的利益，单方面放弃了日本对我们的战争赔偿，在当时六百多亿美元是一个什么样的概念？而现在我们民间的对日索赔工作一再的受挫，这和当年中共放弃日本赔偿的卖国行为是有着根本原因的。正如前面所说的，一个自己都不爱惜自己的同胞的民族又怎么能让外族来爱惜自己的同胞呢？中共代表国家放弃了日本应该给予我们的战争赔偿，政府都不支持，日本还能害怕中国的小小老百姓吗？</div>

							
							
							
							
