『天涯杂谈』 所谓爆料印度奶粉孕酮0.7μg/kg被退货文章之逻辑错乱<br />
点击：1091&nbsp;&nbsp;回复：50<br />
作者：我自横刀向天啸11&nbsp;&nbsp;发表日期：2010-8-19 10:51:00<br />
&nbsp; &nbsp; 有网友爆料中国商贸网一篇报道：黄岛局从印度进口的乳制品中检出孕激素类药物残留<br />
　　<br />
　　 9月23日，黄岛检验检疫局从一批自印度进口的脱脂奶粉中检出孕激素类药物孕酮呈阳性，检测结果为0.7μg/kg。根据不得检出的标准限量规定，判定该批货物不合格，作退运处理。该批脱脂奶粉原料产地为印度，总重102,000千克，货值总计340,680美元。<br />
　　 孕酮，又称黄体酮，是由卵巢黄体分泌的一种天然孕激素，在体内对雌激素激发过的子宫内膜有显著形态学影响，为维持妊娠所必需，若过量或长期食用，则可能出现雌性化现象。<br />
　　 鉴于近期黄岛口岸国外乳制品进口量剧增，黄岛局不断加大对进口乳制品检验检疫力度，严防不合格产品进入我国市场。同时建议进一步完善我国乳制品及含动、植物蛋白食品的相关卫生标准，加强风险分析，保证进口食品安全卫生。<br />
　　<br />
　　 此文一出，舆论哗然，刚刚平息的“激素门”又闹得纷纷扰扰。 论调是印度奶粉0.7μg/kg被退货，患儿家中存留样品内源性孕酮检出值33μg/kg，铁证呀，不合格呀，很多人兴奋得鼻子都红了！<br />
　　 对不住，我是较真的人，我对此论调强烈质疑！花了两个多小时上网搜索，因为开的窗口太多，造成电脑挂掉，还要计算和转换计量单位，终于摸出点门道，供大家参考讨论。<br />
　　 首先，要质疑报道文章的准确性权威性。这个突然之间出名的中国商贸网原来是个默默无闻的商业网站，无闻到什么地步？各大搜索引擎都搜不到它的网址链接，出名后谷歌才勉强加了个链接，但此文章仍然各大搜索引擎都搜不到，只有所谓的转载，不知道转载者当初是怎么找到的？<br />
　　 网上查到一篇科技论文，可以证明此文章的数据不实、逻辑不合！<br />
　　<br />
　　 市售纯牛奶乳清雌二醇和孕酮含量分析 发表时间：2010-05-29发表者：徐庄剑 (访问人次：154) 该文已刊登在《食品科学》2010, 31(6): 252-254. 徐庄剑，马亚萍，李莉华，王 丰，樊 黎，庞兴甫，谢书艳（无锡市第四人民医院 江苏 无锡 214062) ......<br />
