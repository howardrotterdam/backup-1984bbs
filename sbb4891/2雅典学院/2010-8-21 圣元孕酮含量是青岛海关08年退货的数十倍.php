　　 本研究显示市售纯牛奶品牌间乳清P4存在差异，且同品牌不同批号波动较大，批号间乳清TE2也有一定差异，此种差异很可能与妊娠奶牛奶含量或奶牛所处妊娠时期有关。各品牌间差异均无统计学意义......<br />
　　<br />
　　 由此论文数据计算，内源性孕酮检测结果样品中间值为22μg/kg，这个数值肯定是合格的，那所谓印度奶粉孕酮0.7μg/kg被退货的数据可信度基本为零，否则早就闹出国际贸易争端了。<br />
　　 如果要找一个合理的解释：从检出孕激素类药物呈阳性推测，药物残留为醋酸甲孕酮等禁用的外源性性激素，只要被检出，即是不合格。而有些人在故意利用不权威网站的不严谨文章模糊内源性和外源性的差别，偷换概念制造事端。<br />
