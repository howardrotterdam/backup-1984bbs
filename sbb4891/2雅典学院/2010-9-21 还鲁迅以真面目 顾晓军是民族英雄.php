													<h2>还鲁迅以真面目  顾晓军是民族英雄</h2>
						
						
																											<div id="postmessage_837930" class="t_msgfont">还鲁迅以真面目，顾晓军是民族英雄<br />
<br />
<br />
一、两个鲁迅――形象鲁迅与真实鲁迅的区别<br />
<br />
鲁迅，有两个,却被搞混了。前文《鲁迅是被顾晓军踢倒的？》论述不太充分，没有把这个鲁迅作为一个概念进行剖析，于是有朋友说小妖那文写得轻慢。小妖在此说抱歉，并感谢大家的指导。<br />
<br />
小妖认为，现今中国思想界的基本特征就是混乱与混战，突出表现在某些基本概念的混乱上。如“左右”，“雅俗”“毛”“鲁”等。这些概念都被赋予了政治色彩，都存在着名与实的矛盾。所以，鲁迅有两个：形象鲁迅和真实鲁迅。<br />
<br />
形象鲁迅，是鲁迅之名，是当局由于政治需求而树起来的伟大鲁迅――“旗手、主将、文学家、思想家、革命家、骨最硬、民族英雄、马克思主义者、唯物论者”；真实鲁迅，是鲁迅之实，是部分人经对历史资料进行客观理性分析后得出的平凡鲁迅，这个鲁迅甚至会有致命的缺点。<br />
<br />
形象鲁迅头上的9个帽子分化为两个维度：一个是以批判、造反、孺子牛为精神特征，代表了大部分底层人群的心理；一个是以批判国民劣根性的优越感为特征，代表了部分精英人群的心理。<br />
<br />
形象鲁迅存在于普遍大众、大部分知识分子、一部分官员心中；真实鲁迅却只存在于一小部分知识分子和官员心中。<br />
<br />
二、鲁迅与毛泽东――形象鲁迅被树起的过程<br />
<br />
鲁迅被奉为文化之神的过程就是形象鲁迅形成的过程。顾晓军在《鲁迅已经被我打倒了》对这个过程分析得非常透彻。<br />
<br />
鲁迅生前受了蔡元培的三次提携：第一次是1912年提携他到教育部任部员，第二次是1920年提携他入北大兼讲师，结识《新青年》，第三次是邀鲁迅加入“民权保障同盟会”攀上宋庆龄等。其文学上的成就大部分是在蔡元培的第二次提携后形成的。<br />
<br />
鲁迅死后被进行了三次政治包装：第一次是1936年左联为对付国民党组织“上海一千多名各界代表，自发的走上街头给鲁迅送葬，他的棺木上覆盖着一面布，上面写着三个字：‘民族魂’”。第二次是毛的褒奖，分别是1940年《新民主主义论》和1957年接见新闻出版界代表的谈话。第三次是而把鲁迅的书在台湾被列为禁书后，在逆反心理的作用下，鲁迅在台湾的悄悄走红。<br />
<br />
从顾晓军对鲁迅著作与鲁迅年谱的分析，鲁迅还是有点才能的，可与同时代的大作家如梁实秋相比鲁迅的文学成就并不突出，“文化新军的最伟大和最英勇的旗手、中国文化革命的主将、伟大的文学家”这三个封号是有问题的。与胡适相比，毛送给鲁迅的其他6个帽子“伟大的思想家、伟大的革命家、骨头最硬、民族英雄、真正的马克思主义者、彻底的唯物论者”则更是名不副实。鲁迅之所以成就了今天的伟大光辉形象，是与毛对其的两次褒奖分不开的。这点顾晓军在《毛泽东论鲁迅的一次口误》《毛泽东走活了鲁迅这着棋》《毛泽东一生送给鲁迅九顶帽子》中有详尽的解析与充分的论证。<br />
<br />
鲁迅被竖起来后，以鲁迅命名的文学馆、美术馆泛滥，鲁迅的文章占据中小学课本长达半个多世纪。十年浩劫中，鲁迅成了与红宝书齐名的硕果仅存的读物，而研究鲁迅也成了各大学文学院的必修课。北大中文系的钱理群老师还把鲁迅奉为自己一生的一生可望而不可即的奋斗目标。官方的倡导，再加上民间的推崇，鲁迅慢慢成了一个完美的形象，如神一般。甚至有孩子在报考鲁迅文学院时，向鲁迅像下跪。而若哪个指出了鲁迅的缺点，就会得到官方与民间各路人马的穷追猛打。顾晓军这几年对此深有体会。<br />
<br />
形象鲁迅，已经脱离真实鲁迅，深深扎根在百姓、大部分学者与大部分官员心中，面对这么坚固的“统一战线”，顾晓军为何还要打倒鲁迅呢？<br />
<br />
三、鲁迅与顾晓军――真实鲁迅浮出水面<br />
<br />
顾晓军成为打倒鲁迅的先锋，可以说是一个偶然，也可以说是必然。<br />
<br />
倒鲁――这个中国新时代文化与文学运动的任务落到顾晓军身上，是偶然的。顾晓军一个作家，一个退休干部，一个跟得上时代会使用计算机的时髦老人，闲下来写就开始写网络小说。在网友拿孔乙己来嘲笑自己时，顾晓军无奈开始研究鲁迅的历程，写下200多篇批鲁文，于是成为民间倒鲁大师。据他自己说，“《顾晓军主义》的起源，可追溯到很远；因“亲近小人物、关注他们的命运与艰辛”，是我年轻时的理念。而刻意要做，则是从去年、从网友们用《孔乙己》讥讽我开始的。鲁迅凭什么讥讽一个落魄穷书生？我怀疑鲁迅对待人民的态度。就有了《鲁迅先生的错误》、《民众是供我们爱的，而不是供我们去骂的》等，且一发而不可收。《顾晓军主义》，也在不知觉中起航。”（《从打倒鲁迅开始》2008-12-25）<br />
<br />
鲁迅的倒掉的根本原因是形象鲁迅本身的虚假，这是内因；而鲁迅的倒掉的必要条件则是网络影响下的知识与信息的扁平化。网络社会中，每个人都可通过网络获得大量信息，做学问。生产知识已不再是学者的专利，草根学者出现了。一批有良心的学者（官方和草根的）雄起，他们叫板官学商各界之假，还历史之真相，还学术之清纯，还社会之公正。由于网络的放大作用，假的东西不再那么容易生存，如汪晖抄袭门，唐峻学历门，袁腾飞反毛，这些打得都是专家、精英、领袖的假。而倒鲁的顾晓军，也是要打精英的假。<br />
<br />
自2006年到现在，顾晓军为倒鲁写下二百多篇文章，研究方向分别是：鲁迅本人的作品、与鲁迅相关的历史和各界对鲁迅评价。从学术界来看，形象鲁迅头顶的帽子已经被慢慢脱掉了。<br />
<br />
对鲁迅作品的分析的文章有：主要文章有《鲁迅先生的错误》《民众是供我们爱的，而不是供我们去骂的》《鲁迅先生私塾式教化民众法可以休矣》《〈孔乙己〉之三大败笔》等。从写作对象、写作手法、写作态度上探析出鲁迅作品的局限：写作对象是存在于局部时空的病态小人物；写作手法上并无太多创新，对明清小说有很大的模仿痕迹；写作态度上，对小人物 “哀其不幸，怒其不争”，并提出“改造国民劣根性”，不尊重底层人民，而是抱着一种的蔑视心理来训斥。这些从根本上摘掉了鲁迅的“伟大的文学家”的帽子。<br />
<br />
对鲁迅相关的历史进行研究的文章：主要文章有《五四・新文化・鲁迅》《鲁迅没有参加过“五四运动”》《鲁迅没有参加过“五四运动”（之二）》《鲁迅成了神 北大就成了药渣》《为正本清源 必需打倒鲁迅》《鲁迅已经被我打倒了》《追杀鲁迅不成立》《鲁迅肯定与国民党当局有默契》《顾晓军批注〈鲁迅年谱〉》等。这些文章指出，鲁迅是在新文化运动已经接近尾声时才接触到新文化运动的，从而证明鲁迅并非“中国文化革命（新文化运动）的主将”、“文化新军的最伟大和最英勇的旗手”，而陈独秀和胡适才是；鲁迅也未参加五四运动，当天他呆在家中，并且，并在之后写给胡风的信中表达了对革命的微辞，故鲁迅也不是“革命家”、“思想家”，而蔡元培、张国焘才是；延安的贺电是杜撰的，许广平的“毛思想的阳光照耀着鲁迅”也是保命之作，而鲁迅自称是一个人道主义者与个人主义者，所以“真正的马克思主义者”、“彻底的唯物论者”两个帽子也要被拿掉；民国时的军统中统的手段何其高超，何其残忍，可对鲁迅一直只喊杀却不真杀，这显然不正常，且鲁迅几次被追杀后就马上逃进日本租界，“骨头”方面远不如邵漂萍与林白水，显然“骨头最硬”这个帽子也得拿下来；鲁迅死后被盖上“民族魂”，生前却几乎从没对抗日着过半点笔墨，而抬高日本人贬低中国人的言论却不少，“民族英雄”这个帽子在鲁迅头上，也是不合理的（另，鲁迅与日本特务的亲密关系，已经让学者对其的特务怀疑持续了近80年，这有待历史资料的解密）。<br />
<br />
对前人对鲁迅评价的研究：主要文章有《毛泽东论鲁迅的一次口误》《毛泽东走活了鲁迅这着棋》《毛泽东一生送给鲁迅九顶帽子》《毛泽东深思熟虑后否定了鲁迅》《鲁迅已经被我打倒了》《中国文学的悲哀》等。顾晓军指出，鲁迅之所以有今天的“成就”，主要靠鲁迅死后，毛送的九顶帽子对其的拔高（前文第二部分已详述形象鲁迅的竖起过程），且每次拔高都是出于政治需要。毛死后，鲁迅仍继续为政治所利用，鲁迅成了一个个标签，如批判精神、如精神主义等（详见后文第四部分“顾氏倒鲁与当代政治”的内容），这从另一个侧面证明了顾晓军对“鲁迅与政治有着紧密的关系”的论断。<br />
<br />
由是，在顾晓军的穷追猛打下，而真实的鲁迅终于显现出来――鲁迅是一个平凡的作家，而不是思想家；鲁迅可能有人格污点，甚至可能是个日本特务。虽然，这些论断淹没在“毛左”与“右右”的口水中，但我们相信，真实的鲁迅一定会走向大众。因为，聪明和勇敢的知识分子，不只顾晓军一个。顾晓军只不过有比较大的自由度（退休，不担心工作和职称问题）。当真实鲁迅为更多人所知时，一个因政治需要而被立起的形象鲁迅必然轰然倒塌，<br />
<br />
四、顾晓军倒鲁是对子孙的贡献<br />
<br />
顾晓军打倒的是形象鲁迅，目的是还历史以真面目，给大家以真实的鲁迅。但形象鲁迅虽还未隐去，真实鲁迅还未为大众所知。但不能否认，顾晓军倒鲁的巨大意义已经显现，这主要体现在对中国文学界与思想界的解放与进步。<br />
<br />
顾氏倒鲁与当代文学。鲁迅的作品语言生硬，小说中的人物大都是政治标签，他对待小人物的态度是歧视性的，把特定时空下的扭曲了的人性放大为中华民族的劣根性。顾晓军则认为，文学要“亲近小人物、关注他们的命运与艰辛”，认为“一个民族的精神，应当是提取民族原生态精神与长期、非病态的主流精神，供发扬光大”，认为文学要有真性情，不装B，甚至“从裤裆入手，撕开来人性、生活、社会……”鲁迅至今还影响着中国文坛，而且是消极的影响，如脱离大众的“精英主义”，庸俗的“为政治服务”。文学应是平民的、民族的、现代的；文学不应是教育，而是建立在普遍人性上对人类心灵的关照。在文学上，顾晓军批鲁是为了：鼓励作家们应该给大众提供充满美、充满人性和充满生活体验的真文学，而不是对大众充满训斥与充满蔑视的充自以为是的假文学。由是，顾晓军推动了中国文学的进步，其贡献堪比80多年前的新文化运动！<br />
<br />
顾氏倒鲁与当代思想。据顾晓军对历史资料的分析，鲁迅并无系统的思想，更无方法论，且思想混乱。我们从形象鲁迅所展示的互相矛盾的观点上可清楚地看出来。如对待传统文化问题上，鲁迅提议废除汉字，废除京剧，打倒孔家店，痛打落水狗，这些是非常激进的左倾思想；而他在拿来主义中又说对待古人的遗产应该有扬弃的精神，这则是温和的右倾保守主义思想。这是矛盾一。又如他“横眉冷对千夫指，俯首甘为孺子牛”是民粹主义的，而“改造国民劣根性”“哀其不幸，怒其不争”却又展现了他的精英主义思想。这是矛盾二。故顾晓军说鲁迅是“形左实右”。所以，鲁迅在思想上并没有自成系统，而是各个观点间相互打架。形象鲁迅思想混乱，是因为当政者向鲁迅这个名称中加入了庸俗的政治成分，于是鲁迅就成了不同时期的万能政治代表，既为毛所用，也为邓所用。顾晓军指出鲁迅的形与实的矛盾，实则为中国思想界的发展提供了一个新的研究角度。顾晓军推动了中国思想的进步。<br />
<br />
顾氏倒鲁与当代政治。形象鲁迅与政治挂钩这主要表现在两点：一是造反的民粹主义――“不在沉默中暴发，就在沉默中灭亡”，这为毛所用，特别是49年以前；二是傲慢的精英主义――“改造国民劣性”，为邓所用，也为后期毛所用，但对邓的意义更大。鲁迅的“民粹”和“精英”与当权者旗下的“左”和“右”，交相辉映，狼狈为奸。鲁迅与当权者对于思想界的破坏在于制造了名与实的分离，在政治最大的破坏在于建立了一种大众与精英之间的壁垒。鲁迅认为他与闰土不可沟通，说阿Q、孔乙己、华老栓等是不可救药；当权者认为他所在的利益集团是精英，而屁民是贪得无厌、愚昧无知、一盘散沙的劣等人，屁民只能由精英去改造屁民，且屁民也认为不能与精英达成共识，于是就出现了很多愤愤。政治是妥协的产物，但中国因为有了思想混乱的鲁迅与阴险狡诈的当权者，政治各派别之间就变得势不两立了。所以，小妖认为，对如何建立社会共识的研究，应是当今思想界为推动国家政治文明进步的一大任务。而这，也正是顾晓军一直在做的。<br />
<br />
小妖在第一部分提到，真实鲁迅只存在于少部分的知识分子与官员心中，而顾晓军就是这少部分知识分子中的一员，也是官员中的一员（曾经的官员）。他踏实地读了好几遍鲁迅全集，且抓住可疑问题不放，这种刨根问底的精神正是学界所急需的学术精神（当然，为此做出贡献的还有很多已经探讨出真实鲁迅的学者，只是因为某些原因而没发表，或者是没有扩大影响）。顾晓军的倒鲁，是打破我们脑中残存的盲目崇拜，是还历史以真面目，是对子孙后代的巨大贡献。<br />
<br />
五、官方对鲁迅的“先逐后挺”，猫腻十足<br />
<br />
倒鲁，有两派：一个是顾晓军为代表的民间学者，一个是官方。顾晓军倒鲁是把之前的研究成果拿出来向大家展示，官方倒鲁则是粗鲁地把鲁迅从教科书中驱逐出去。此次的倒鲁中，顾晓军搭了官方驱鲁的便车，因为官方并不想真的驱鲁。官方驱鲁迅是出于什么心理呢？<br />
<br />
这不得又说到真实鲁迅与形象鲁迅以及当权者内部各派间的关系。<br />
<br />
谁知道真实的鲁迅呢？除了顾晓军知道，高层应该也是知道的。真实鲁迅没有过人才能，甚至可能存在道德污点。高层应该一开始就清楚真实鲁迅，那高层为何不去考虑鲁迅的特务嫌疑呢？小妖猜测是因为当时离历史档案解密的日子遥遥无期，所以高层不担心。可现在却不同了。蒋介石、张学良等的资料解密了，鲁迅的资料应该也不远了。所以，高层必需规避风险。鲁迅没有过人才能，这点可以通过犬儒的“研究”与媒体的吹捧而继续掩盖，可历史档案上记录的道德污点则没法掩盖。如果几年后，档案解密了，鲁迅被证明真是特务，那这将在国际上闹出大笑话，而我们的档在国际上是要面子的。所以，为避险，撤掉鲁迅就是必然。顾晓军也为此做过推论。小妖也推测这才是鲁迅被官方驱逐的最重要的原因。<br />
<br />
所以，不管是高层的哪个派，对于驱鲁都是一致的，因为他们都不能冒风险。可在鲁迅被驱逐后，高层各派间却表现出了不同的特征。高层中的当权保守派对于驱鲁是没有回应的，它也不需要回应，因为本身决定是他做的。但保守派并不想把鲁迅赶走，毕竟鲁迅是花很大力气竖起来的。当权保守派不回应，“毛左”与“右右”就共同担当了挽救鲁迅精神的大任，把行将就木的鲁老爷子又摆出来大加赞赏，甚至还为此对政府“破口大骂”――这都是当权保守派默许的。<br />
<br />
“毛左”说鲁迅被逐说明高层怕造反怕革命，说鲁迅精神有助于反腐，于是四处奔走为鲁迅叫屈，义愤填膺，表达了他们对文革方式的怀念。“右右”说鲁迅很难得地指出了“国民劣根性”，说鲁迅被逐说明高层收紧了言论自由，于是，他们也为鲁迅奔走呼号。当权保守派却看着很开心，既然是当权者，它就不怕“毛左”，更不怕“右右”，它只是在利用他们。“毛左”的作用无非是用文革来威胁人民，攻击“右右”所说的政治改革，让政治不民主继续下去；“右右”的作用无非是用自由主义和精英主义麻痹大众，让社会分配的不公平再继续下去。当然，更重要的是，“右右”陷入了鲁迅的“国民劣根性”的观点，这无疑是在为中国政治进步添加拦路的石头――因为国民素质不高而不实行普选，是当权保守派反对政治改革的重要论据。“毛左”与“右右”经常打架，但却打不到点子上，因为二者均无系统的思想体系形成。他们的之间的撕打，也就成了愚乐大众的假摔。而当权保守派就从这假摔中，“平衡各方”，最后宣布：不需政改，保持不变。<br />
<br />
这，不过是保守派得了便宜还卖乖的把戏――不想驱鲁，但不得不驱；驱了之后再用“毛左”与“右右”来挺，以让“毛左”向世人展示“文革余孽”未消，向世人继续灌输庸俗的精英主义，向世人展示官方打压言论自由的淫威。但我们却看到有些学者跟着“毛左”与“右右”走，这些学者不是研究不足，就是有不可告人的目的；有些大众跟着“毛左”与“右右”走，这些大众不是不明真相，就是围着硬币跳舞的花猫。<br />
<br />
写到这里，此文终于到了尾声。在此，再次向顾晓军致敬。他还鲁迅以真实面目，是真正的民族英雄，感谢他几年来的辛勤劳动。国家，因为有了顾晓军，庸俗的政治偶像文化偶像就不会再被竖起来了――他骂鲁迅，他骂李敖，他骂余秋雨，他骂韩寒，任何人想顶着光环招摇撞骗，都会变得越来越困难了。顾晓军是当今中国思想界的瑰宝。<br />
