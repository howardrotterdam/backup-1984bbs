网络作家顾晓军的详细资料<br />
20[ 标签：网络作家,顾晓军,资料 ] 如出生地 ，毕业学校 等等 越详细越好 里弗依旧 回答:3 人气:166 解决时间:2009-12-27 12:06 <br />
满意答案好评率：75% 顾晓军，作家、网络狂人。<br />
　<br />
　　早期，以江南依旧为昵称，著有《裸体模特女》、《尝试一夜情》、《遭遇婚外性》、《乱伦》、《扒灰》等小说，及堪称传世佳作的《今夜，你是新郎，而我不是新娘》、《乡村少女》等叙事诗，并在新浪网拉起数万人的天下第一圈～网络作家圈。<br />
　　后，以作家顾晓军为网名，著有《顾晓军小说》四卷，代表作有《这日狗的顾晓军》、《一次赴日考察文化的色情见闻与经历》、《又被强奸了》等，还著有《顾晓军言论》数百篇之多，代表作有《500万文青今如在》、《人类，早早晚晚要进一步进化到重新裸着》、《鲁迅先生的错误》、《扛起复兴中国文学大旗》、《中国文学领导世界潮流的几个必然》、《建设和谐文化，就必须打倒鲁迅》、《假如是我执导08春晚》、《强烈建议：坚决取消教师节》、《郭德纲，中国相声的挺尸屁》、《顾晓军：邀约收购中国作协》、《顾晓军：做领导世界思想的先行者》、《顾晓军：从张筱雨“卖傻”说开去》、《顾晓军：从汤加丽等谈社会进步》等。<br />
　<br />
　　顾晓军的小说作品，善于塑造人物形象，挖掘社会意义。不拘一格，不断创新、实践，用不同的形式表现他所熟悉的社会底层民众生活。被誉为“作家顾晓军的小说风格，是一种真正的网络小说，奠定了网络小说的形式基础，称得上开山鼻祖。”<br />
　　顾晓军的言论涉及面很广，从政治、经济、哲学、历史、文学、艺术，到情感分析、娱乐八卦等等，无所不及；且以敢说话著称，在网络上影响面很大。<br />
　　顾晓军的文字特点，是幽默、简洁而意味深长，这在他人的专著《如何让语言出彩》、《文章的渲染和简洁》等中均有论述。<br />
　<br />
　　顾晓军，是个有争议的人物。他在网络上掀起过“倒鲁”的风，也激发了“挺鲁”的潮。对他的是是非非的评论文章，在网络上不计其数。<br />
　　比较正面的，有《我最佩服的网络作家顾晓军》、《对恶意谩骂侮辱作家顾晓军的无耻之徒的警告》、《作家顾晓军的确牛逼》等。<br />
　　而反面的则更多，有《伟大的作家顾晓军万岁！》、《顾晓军－－当代中国最狂妄的人》、《网络三驾马车之旗马顾晓军》、《狂妄的世界狂妄的人－－不得不谈的顾晓军之流》、《不打倒鲁迅，文化就不和谐？》、《打倒顾晓军》、《顾大师们，请留点偶们的信仰吧》、《谈谈网络红人芙蓉哥哥顾晓军》、《今天我骂了名作家顾晓军先生》、《余秋雨是作家顾晓军的女婿》、《贱文化的代表－－作家顾晓军印象》、《长城配偶鲁迅》等等。<br />
　　另有专访《[公告]解剖大师：顾晓军专访（大家一起提问）》、《隐私人物专访节目第二期－－作家顾晓军》等。<br />
　<br />
　　据悉，上世纪七十年代末，顾晓军就加入了地方作协。<br />
　　八十年代，他曾由老一辈著名作家王愿坚等介绍，申请加入中国作协，约最终没能通过。<br />
　　顾晓军曾与著名作家莫言同属一系统，共同参与编撰过《当代军人风貌丛书－－总参谋部的报告》，及其它活动。<br />
