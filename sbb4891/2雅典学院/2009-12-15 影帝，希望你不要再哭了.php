													<h2>影帝，希望你不要再哭了</h2>
						
						
																											<div id="postmessage_375443" class="t_msgfont">在最近召开的全国经济工作会议上，听说温家宝总理又流眼泪了，我没看过相关的视屏，不知道他这次难过的具体原因，但汶川地震时，我还在国内，真的通过电视看到他流泪了，我很感动，也和他一起哭泣，后来他又在不同的场合流泪，比如为进城打工的农民工讨薪等，我震惊之余，开始流汗了。现在，他流泪已不是什么新闻了，人们似乎已经被他流泪流得麻木不仁了，我想，他为什么一次又一次地用流泪显示心底的软弱呢？他为什么不学得强硬一点呢？难道人大代表被动地选举他当上国务院总理，就是让我们看他流眼泪吗？<br />
<br />
无疑地，中国目前已成为老百姓流泪最多的地方，不用说别的，就拿去北京上访的群众来说，每天，每一秒，都有被殴打，阻挠而流泪的无奈的人们，原本中央设立信访办就是接待百姓投诉的，但各个省市地区却都在胡锦涛和温家宝的眼皮底下抵抗他们，这些官员私设了黑监狱，专门用来截访和抓捕访民，由于中央斗不过地方，信访制度已形同虚设，所以由流泪到流血的人越来越多，其中还有的被政府官员打伤至死至残，这种案例比比皆是，不胜枚举，甚至还有一个姓李的女访民，在黑监狱被黑看守强奸了，有的访民忍无可忍，只好倾激愤于暴力，比如杨佳，在杀了几个警察之后，被判了死刑，我想总理温家宝不能不知道吧？对此他心里不难过吗？<br />
我不否认，他是二把手，有许多事情受制于人，无能为力，可是象上述访民这种事故，他难道不能忍一忍自已的眼泪，咬咬牙，亲自到信访办去几次，查一下黑监狱，用训斥和法办取代怜悯和流泪吗？原本地方的封疆大吏就无法无天，不把他放在眼里，他再动辄流泪，岂不是鼓励他们变本加厉，肆无忌惮吗？我不相信他如果抓住访民投诉无门的典型事例，严惩渎职犯罪的贪官，就能因此下台？这到底是怎么回事呢？<br />
<br />
其实，对他和中共高官来讲，应当流泪的事情实在是太多太多！多如牛毛！在我看来，他应当选一选，选出更让人难过的事，比如成都动迁户唐富珍的自焚，她为了捍卫自已的家圆，和公权力进行了不屈不挠的斗争，最后以&quot;人与楼房共存亡&quot;的悲剧收尾，但她死了，家人还要被强加一个&quot;暴力抗法&quot;的罪名关押，我想问温家宝，你看过录相没有？全世界人民都看到了，你流泪了吗？你愤怒了吗？<br />
与此同时，上海维权律师冯正虎由日本访问回国，却8次被拒于上海虹桥机场海关，他不得不滞留日本成田机场 40多天，造成了同类事件史无前例的轰动效应。他上有老母，下有妻小，每天晚上躺在机场的木凳上睡觉，至今还在和警方较力，我不相信温家宝不知道，那么他为什么不流泪呢？为什么不出面解决呢？<br />
<br />
诸如此类的事例很多，如果上网浏览会应接不暇，作为普通百姓看了，出于同情和无奈，流一些眼泪，也是人之常情，是啊，谁叫我们无职无权呢，但作为身居高位的堂堂大国的总理，面对这些令人发指的丑闻，为何不怒发冲冠，拍案而起呢？这真令人匪夷所思！<br />
俗话讲：男儿有泪不轻弹，只是未到伤心处。有人说温总理在做秀，我不同意这种偏见！我不否认温家宝流泪是出于真诚，他流的不是鳄鱼的眼泪，他是真的心中难言而又难过啊！<br />
<br />
是啊，他在&quot;六四事件&quot;发生之时，给赵紫阳当助手，他不仅十分了解内幕，而且对它产生的原因也很清楚，但他眼睁睁地看着王丹等流亡海外的众多有为之士不能回家，&quot;六四&quot;血案不能平反，原中共最高领导人赵紫阳不能正名，他连追悼会也不敢出面参加，内心忍受多大多久的挣扎啊！这是多大的悲哀啊！<br />
<br />
我记得有一次，他在日本访问期间讲到普世价值，讲的不错啊，但回到国内，看到《人民日报》，他的话已被修改得变了味，这或许也是他经常流泪的原因吧！一个国家领导人，都没有言论自由，都不能在共产党办的报纸上刊登自已的真实观点，那么平民百姓的遭遇可想而知！难怪一本《中国农民调查》令他及政治局的人们大为震惊，原来他们和毛泽东一样，由于僵化的一党独大制度，不得不终日生活在谎言和大话之中，他不能不流泪！不得不下决心废除了农业税！。。。。。。但废除的应当是产生这一切社会不公的制度！他敢吗？<br />
<br />
大概就是在这次出访讲话中，他动情地引用了中国著名诗人艾青的诗句：为什么我的眼中总含着泪水，那是我对祖国爱得深沉。。。。。。仿佛温家宝在回应人们对他流泪的指责，他似乎找到了流泪的理由，但他是否知道，当他眼含热泪吟诵这行诗句后不久，诗人的儿子艾未未竟被成都警察暴力殴打，不得不去德国动手术，其原因是他要赶到成都去为黄琦作证，此前艾未未和黄琦等人常年忙碌在地震灾区，他们要调查被豆腐渣工程害死的小学生的人为因素，而温家宝曾也在此地流过泪，那么艾未未和黄魏巫镏有？为何要被殴打？黄琦为何要被判刑？温家宝为什么不为这件不公平的事情流泪呢？我想，假如艾青先生还活着，一定会告诉他：你不要再读我的那句诗了，我说的深爱着的土地，恰恰就是我儿子未未现在去的地方啊！他执着追求的真相，与你何干？你不要再流泪了，你赶快去成都吧，把殴打吾儿的恶警抓起来，把黄琦放了吧！......<br />
<br />
显然，中共十八大日益临近，温家宝的机会已经不多，也就是说，他流泪后有人关注的时候不多了！振作一下精神吧，温家宝！当越来越多的弱者由流泪流汗改为流血的时候，中国就失去了和平转型的良机，陷入万劫不覆的困境，的确以往我们已失去了太多机会，现在必须抓紧！温家宝，不要再流泪啦！拿出你在汶川地震中的勇气，为中国的宪政民主踏踏实实地做点事吧！<br />
