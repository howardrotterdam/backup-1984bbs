窦文涛：新富阶层自己作为一个群体。<br />
<br />
查建英：像这种浙促会，包括我知道有阿拉善，房地产等等富商，他们就开始用自己的钱来做环保，而且他们自己内部开会也很重视程序，这是不是就是一个进步，好的开头，所以我觉得也还是在开始吧。<br />
