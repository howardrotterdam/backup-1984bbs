																											<div id="postmessage_377961" class="t_msgfont">吴晓波：仇富情绪激烈 有钱人不反思可能被干掉<br />
2009年12月17日 07:42凤凰卫视<br />
凤凰卫视12月16日《锵锵三人行》节目以下为文字实录：<br />
<br />
窦文涛：《锵锵三人行》。很高兴迎来《跌荡一百年》的作者吴晓波。这个《跌荡一百年》，我看了半本。<br />
<br />
查建英：上册。<br />
<br />
窦文涛：但是我觉得你讲中国企业家的历史很有意思，而且我觉得你是不是贯穿始终的，有这么一个观点，你觉得中国自古以来，政府干预经济的习惯，很突出？<br />
<br />
吴晓波（经济学者）：对，其实我们到了1994年，曾经开过一个十四届三中全会，讨论一个问题，说什么叫宏观调控，讨论这个问题，就是怎么干预企业。当时说什么调控呢？叫总量调控，然后加上市场调控，控制总量。但到后来，慢慢变调儿了，这些年开始，中央政府开始把手伸到微观经济里面来。<br />
<br />
窦文涛：但是有人讲，说中国万岁，中国将来成功之后，历史学者们总结起来，说肯定中国走的是一条从来没有人走过的路，等于说是它自己的一条路。所以我觉得现在思想上也很混淆，什么“国进民退”、“民退国进”，这个东西难道有什么神圣的最高原则吗？还是说大家都是摸着石头过河，会不会也有适合中国自己的一种形势？<br />
<br />
查建英：而且不管怎么说，还是一个相对良性的一个中国特色，就像你说宏观调控，说四五年、七八年来一次，当然毛主席是说文革是七八年来一次，那个咱们真的受不了，七八年再来一次文革。但是七八年再来一次宏观调控，好像是属于一个相对来说，大家可以接受，可以忍受，而且好像调控完了，还能发展。多少说中国经济要沉船，要崩溃的，最后我们看了这么多年了，十几年，真的这弯又转过来了，怎么回事。<br />
<br />
窦文涛：咱们这片神奇国土，就能起死回生。<br />
<br />
吴晓波：中国现在有一个什么问题呢？经济学家在讨论一个事，我们政府该怎么来管企业？其实1978年以后，就有两派意见吵得很厉害。一派意见就说放权，我们计划经济不好，国家连颗螺丝钉都要管，放权。所以最早你看放什么呢？把土地放给农民，那个很成功，土地联产承包责任制。<br />
<br />
查建英：对。<br />
<br />
吴晓波：一下子把粮食问题给解决掉了，很成功以后就提出一个说法，叫“包”字进城。<br />
<br />
窦文涛：包子？<br />
<br />
查建英：卖包子。<br />
<br />
吴晓波：“包”这个字，“包”字，承包制。<br />
<br />
窦文涛：三自一包的那个包。<br />
<br />
查建英：包产到户。<br />
<br />
吴晓波：包字要进城，从1984年开始，企业搞承包制，然后地方财政税收搞承包制，当年搞得叫五个大包干，都是包干。大包干好处在哪里呢？地方积极性超级发达，大家都在搞经济。坏处在哪里呢？两个坏处，第一个呢，中央财政没钱了，讨伐财政，到八十年代末的时候叫讨伐财政。<br />
<br />
第二个，由于各地发展，它都以邻为壑，我要发展我的经济，我跟中央承包的，我跟旁边的省，比如我有煤矿，我就不给你。<br />
<br />
查建英：对。<br />
<br />
吴晓波：比如浙江和江苏抢那个蚕茧，新疆的棉花就是不给你，东北的东西就不运进关来，当年叫“诸侯经济”，全部放权以后，就出现“诸侯经济”，而且这个在别的国家发生过大问题，就南斯拉夫，叫“多中心的国家主义”，都是共和国，联邦的就很穷，最后就解体掉了，全部放权的一个改革。还有一派就是收权，不好，咱们收回来，收回的问题是，一收回来大家就死在那儿了。<br />
<br />
查建英：对。<br />
<br />
窦文涛：咱们中国人是不是还是喜欢个体经营。<br />
<br />
查建英：有一个说法是，中国人一管就死，一放就乱。<br />
<br />
吴晓波：一乱再收，一收就死，死了再放。<br />
<br />
查建英：在两极当中，好像有点长不大的孩子的感觉。<br />
<br />
吴晓波：对，到了八十年代末，九十年代初有了一批经济学家，他们发明了一种新的办法，说你全部放也不对，全部收也不对，怎么办呢？收放相结合。90年代初，朱F基政府以后就搞缩放结合。收什么东西呢？就是把税收收上来，中央财政一定要收上来，中央财政收上来造成的结果是什么呢？分税制，我把好收的税，我全部收上来，地方你们没钱，我也管不了什么事，后来地方政府到土地里去拿钱。<br />
<br />
窦文涛：卖地。<br />
<br />
吴晓波：土地属于二财政，它不属于整个财政体系。好处是什么？中央集权统一形成，中央变得越来越有钱。中央财政的收入每年以百分之二十几、三十几的速度增长，超过GDP的两到三倍，这是好处。然后，收了以后，但也不是全部都收，我还要放权，这叫什么呢？叫经济性放权。所有的企业这些事，中央都不管，全部交给市场去管。当年设计了这么一个模型，财政收权，经济性放权。这个模型，曾经给中国带来十多年的高速发展，一方面中央财政很大，权利很大，地方经济也很发达。<br />
<br />
查建英：也有它的自主。<br />
<br />
吴晓波：它放权嘛，有活力，到后来就出问题了。<br />
<br />
窦文涛：什么问题？<br />
<br />
吴晓波：从汉武帝开始，发生一个毛病，发生一个问题，中央集权以后，产生的问题，中央除了税收以外，它还不够，它一根拐杖不够，还需要另外一根拐杖，我跟地方政府谈判。另外一个拐杖，另一个拐杖叫什么呢？叫资源。这个问题是汉武帝时候就有了，汉武帝死了以后，搞了《盐铁论》。中央集权形成以后，盐和铁中央是不是要所有？咱们上次谈过，讨论N多年，漕运是不是要所有，矿山是不是要所有，银行是不是要所有，外贸是不是要所有。一直讨论到民国结束，新中国成立，不要讨论，来搞中央集权，搞计划经济。但是到了我们1994年以后，中央财政集权以后，出现一个问题说，我们还需要有一些，手上有几把米在手上。<br />
<br />
窦文涛：粮食。<br />
<br />
吴晓波：央企。<br />
<br />
窦文涛：央企，几把米。<br />
<br />
查建英：这又来了，这是几桶米。<br />
<br />
吴晓波：我需要有一些资源在手上，靠税收不行了，所以到后来，国有企业放，一开始叫抓大放小，一开始就要抓大，一开始要抓一千个大，1998年以后，金融风暴以后，提出一个叫“国进民退”。<br />
<br />
窦文涛：对。<br />
<br />
吴晓波：不管怎么样一个逻辑，最后造成我中央一定要抓一些企业在手上，一开始抓一千个，后来抓六百个，六百个也偏过，后来抓三百六十个，三百六十个也偏多，现在抓了一百五十多个。<br />
<br />
查建英：我听说现在国资委管的越来越少，从这点上讲，好像你说不通了“国进民退”，管得越来越少了。<br />
<br />
吴晓波：但是每个东西都很大。<br />
<br />
查建英：对，我这会儿听明白了。<br />
<br />
吴晓波：抓的手上东西，变成说只有我能做，你不能做。<br />
<br />
查建英：垄断性的。<br />
<br />
窦文涛：垄断。<br />
<br />
吴晓波：所以就变成，你比如说今年发生煤炭这个事就是，它是资源，很容易被抓。<br />
<br />
窦文涛：你提出这个，我觉得这事可以具体化一点了，说是山西整合煤矿资源，吴晓波还写了一封“致煤老板的一封公开信”，结果被很多人骂。<br />
<br />
吴晓波：我要通过你这儿辟辟谣。<br />
<br />
窦文涛：他这个信，其实看上去是说煤老板现在国家整合你们，你们现在知道肉痛了，你们自己应该反思一下，为什么不好好对待工人，为什么不保护环境，你这种社会责任，你当时赚钱的时候不考虑，现在因果报应到你头上，一般人骂你是什么呢？<br />
<br />
吴晓波：就因果报应这四个字。<br />
<br />
窦文涛：说你不该骂这些煤老板？<br />
<br />
吴晓波：对，说我骂他们因果报应，缺乏经济学常识，缺乏良性。<br />
<br />
查建英：是不是煤老板也是做了很多好事，除了增加就业机会。<br />
<br />
吴晓波：他们确实是，我去过山西。<br />
<br />
窦文涛：帮中国减少了不少人口。<br />
吴晓波：煤老板在山西做的事很过分。我写这个煤老板什么意思呢？第一我是坚决反对国进民退的，就是说你没有道理。但是我认为什么呢？我前面讲的道理，因为中央集权要求客观的它一定会产生出一些垄断企业，它一定会对资源产生无限的一种欲望，所以国营资本来控制煤炭，是一个政策的逻辑，不是某一个人的事情。<br />
<br />
查建英：因为实际上，就是说你把中小煤矿定在年产30万吨以下，就得收归国有。<br />
<br />
吴晓波：这个是技术问题，把你干掉。<br />
<br />
查建英：而且事实证明，发生这些煤炭事故的，有很多大的国有企业。<br />
<br />
窦文涛：这次108人死亡的这个，大家不就聊了嘛，国企也有矿难。<br />
<br />
查建英：对，三起，不止一次了，三次了。<br />
<br />
吴晓波：最近四次里面，有三次是国营企业。<br />
<br />
查建英：都是大型国企，所以它其实不是一个规模大小，是一个安全。<br />
<br />
吴晓波：它没有理由站不住脚的。<br />
<br />
查建英：对，没有理由站不住脚的。你收归变成大国企以后，照样没解决安全问题，监测问题。<br />
<br />
吴晓波：我这个文章是写什么道理呢，我觉得山西省政府用这个办法来收民营煤窑，已经变成了事实了，我们去谴责它，批评它也没有大用，因为它是一个政策逻辑。我写文章意思是什么呢？我是想给那些企业家跟煤老板们讲两句话。因为中国历史上发生过很多事情是什么呢？这个国家的势力是由三种人构成的，一个就是政府，统治者，还有一部分就是有钱人，还有一部分是没钱人。历史上发生很多次事件是政府鼓动没钱人把有钱人干掉。<br />
<br />
吴晓波：因为中国历史上发生过很多事情是什么呢？这个国家的势力是由三种人构成的，一个就是政府，统治者，还有一部分就是有钱人，还有一部分是没钱人。历史上发生很多次事件是政府鼓动没钱人把有钱人干掉。中国在1920年代发生过，1950年代发生过这个事。现在中国的有钱人慢慢多起来了，这是好事情，中产阶级、企业家阶层多起来了。中国现在企业家阶层现在要想一个问题，当你的利益被剥夺的时候，什么时候没钱人能够站在你的身后？这个现在是一个大的问题，我说为什么山西那么明显的一个被剥夺性事件。<br />
<br />
查建英：对。<br />
<br />
吴晓波：没有一个煤矿工人站在煤老板的后面。<br />
<br />
窦文涛：对。<br />
<br />
吴晓波：山西人民鼓掌，或者最多也就看着你被赶走。<br />
<br />
查建英：对。<br />
<br />
吴晓波：这就是没钱人或者无产阶层面临一个情况，你山西那些煤老板，在你该赚钱的时候，你没有善待你的员工，你没有保护好环境，没有维护好你跟社区的关系，你赚了钱，还去买悍马，到处乱撞人，吃喝嫖赌。<br />
<br />
查建英：对。<br />
<br />
吴晓波：煤老板已经变成一个反社会名词了，我认为这一次煤老板付出的代价是将近2000亿人民币的代价。<br />
<br />
窦文涛：是吗？<br />
<br />
吴晓波：对，浙江的煤老板大概有500亿。<br />
<br />
窦文涛：我不太懂具体的，好像听说这股风吹到河南，有人说河南现在也正在调研，准备搞。<br />
<br />
吴晓波：对，也要搞。<br />
<br />
窦文涛：有的民营矿主现在开始疯卖，卖矿，他就想，与其到时候强制被兼并，还不如我现在自己卖，说现在有一个报导上讲，有一个五千万的矿，他卖了三个亿，光中间人就拿走了一个亿，他说我现在卖还能赚到钱。<br />
<br />
查建英：而且去买它的一定又是国企，大国企。如果这些中小民营的矿都被集中到大国企以后，后果是不是煤会涨价，最后受害的不止是这些小的民营企业家，而且是老百姓。<br />
<br />
吴晓波：对。一般垄断以后，比如说中国的石油的问题。<br />
<br />
窦文涛：天然气的问题。<br />
<br />
吴晓波：天然气的问题，现在水的问题，都是因为垄断，没有竞争，它说涨，你眼睛朝他看看，怎么办呢？过两天又涨了，你再看看，你又不能咬它两口。<br />
<br />
查建英：对。<br />
<br />
窦文涛：我觉得央企、国企，它们不是上面有领导的嘛，民间很多的不满，不能制衡他们吗？不能有所反映吗？<br />
<br />
查建英：不是有煤老板的协会，江浙的这些温州的老板，不有一个叫浙促会。<br />
<br />
窦文涛：生产浙江香醋的是吗？<br />
<br />
查建英：不是，浙江的投资集体的促进会。<br />
<br />
吴晓波：省政府下面的一个半政府性质的团体。<br />
<br />
查建英：他们不能在兼并过程中，发出他们自己的声音吗？<br />
<br />
吴晓波：他们发出了。<br />
<br />
查建英：比如说买卖要公平，我们要有一个产权的问题，包括你说的这个反省这一面，他们有没有一个群体性的一个反省呢？<br />
<br />
吴晓波：现在开了几次会，希望媒体，希望理论界，为他们的利益做一个保障，现在看来这个声音很小，或者没有得到很大的一个民众的呼应，你靠几个经济学家，靠几张报纸解决不了问题。所以我认为它是一个阶层问题，现在中国的企业家阶层，改革开放以后，重新出现一个有产阶层，他们本身决策觉悟性不够。<br />
<br />
窦文涛：你说的这个是，上次我跟几个朋友聊天。<br />
<br />
吴晓波：咱们中国人到国外去一样，如果到处随地吐痰，到处大声讲话，你看那是一个人的事，最后人家说你中国人没礼貌，中国人不应该在这个社区里面，咱们的煤老板其实就是这个道理。<br />
<br />
窦文涛：所以上次聊天有一个学者讲，大概也是瞎说，他说历史上看，如果最后搞到穷人造反，他说这是最坏的事情，因为暴力，血流成河，你从历史上很多这样的例子。<br />
<br />
查建英：对。<br />
<br />
窦文涛：我听他说的有点乌托邦，最好精英阶层，或者说不叫精英阶层，有钱人阶层，你们先富起来了，先富起来的人，能够自省，自我反省，然后由上而下的，你或者拿出你的一部分，或者你有这个社会责任，有这个意识，及早的在这个危险点之前，放缓彼此之间的矛盾，这是好的。<br />
<br />
吴晓波：它这个不是个乌托邦，它就是现代政治的一个基本理论，这个社会一定要有有钱人和没钱人，但是最终要保护的是保护那些没钱人，有钱人身强力壮的，自己会赚钱。怎么来保护没钱人？第一政府要承担责任，他不能饿死，他要享受基本的教育，这是政府。第二，政府要通过税收，通过各种各样的杠杆问题，比如说北欧，收税收的很重，然后去搞社会福利，富人和穷人之间均衡掉了。<br />
<br />
查建英：我觉得这里边，我们说的还不是吃不上的穷人，我们说的煤老板是一些已经第一代的富起来的人，他们的社会责任感，他们的自我意识，阶层的意识，他们没有尽到。<br />
<br />
吴晓波：我认为他们没有觉醒。<br />
<br />
查建英：现在已经逼到他们在自身最大利益被剥夺的时候，他们还不反省的话，不是说这个已经既成事实，其他的民营企业家，还看不到煤老板的教训的话，未来是很可怕，全部的国进民退了。<br />
<br />
吴晓波：你比如说我讲另外一个，如果有可能情况，比如咱们现在煤老板是被他们剥夺掉了，如果有一天，地方财政没有钱了，开始剥夺房地产老板。<br />
<br />
查建英：对。<br />
<br />
吴晓波：全中国老百姓都鼓掌，就应该查你，你把房价炒到五万块一平方米。<br />
<br />
窦文涛：以现在的民意，怪不得他信里面讲得也有道理，这次好像整合山西的煤老板，确实一般老百姓。<br />
<br />
查建英：一片叫好。<br />
<br />
窦文涛：反正至少没有说不好的，因为这么些年来，名声坏了，觉得你老是不干好事。<br />
<br />
查建英：我们能不能相比，俄罗斯也发生过这种事情，就是说普京上台了，一下子把巨富抓起来了，给你们敲山震虎。等于是国企其实又回来了，国家政权又进入到经济领域，而且最后是大型的垄断局面形成了。可是老百姓对这个并没有觉得不好，普京就是在那一阶段，让你所谓的民营企业家，私营放开的时候，你们没干好事，你们最后全是敲诈罗嗦，黑社会。<br />
<br />
吴晓波：以暴易暴。<br />
<br />
查建英：对，以暴易暴，可是这个结果，从一个长远的角度讲，其实它可能是不好的。<br />
<br />
吴晓波：肯定不好的。<br />
<br />
查建英：对。<br />
<br />
窦文涛：怎么不好呢？<br />
<br />
吴晓波：比如说煤老板肯定做了错事了，他破坏环境，咱们不去讲它了。你现在这样的“国进民退”的方式，没有契约精神，当年你政府和煤老板怎么签协议的。而现在宪法说矿山是国有化的，当年这个协议是不应该给你签的，现在是这样一个逻辑，这个逻辑不但发生在这个地方，现在很多地方发生了什么呢，比如说有线网。<br />
<br />
以前有线电视网接入的时候，政府没有钱，广电部门没有钱，怎么办呢？跟民营企业讲，你来修，修了还不赚钱，这个事十年前开始干的。为什么呢？因为你每家每户，交每个月的有线电视收费，只能收几块钱，就是你接进去，你花了一个亿，改造了一个区域的有线网，你也不赚钱。好了，现在赚钱了，第一这是一个垄断性资源，接了以后，不能跟第二个了，只能够用我的。第二个，原来两块钱变二十块钱了，一年可以收两百多块钱，马上变成一个暴利型的东西。现在国内发生了什么事情呢，很多省的地方，跟山西省政府一样的，跟你说，文涛，当年那个协议，是违反法律的，所以不能作数。<br />
<br />
查建英：我收回来了。<br />
<br />
吴晓波：这样，你先投了一个亿对不对，我再给你加三千万，请你马上走人。我就不走，你不走，你知道我们这儿有一个地方叫税务局吗？<br />
<br />
窦文涛：这家伙你跟国家玩，你玩得过嘛。<br />
<br />
吴晓波：对，它就是没有契约精神。<br />
<br />
查建英：对<br />
窦文涛：他讲这个契约，我不禁想起那么一个意识，胡适当年，当年不是把宣统皇帝溥仪，民国政府赶出宫了，赶出去了。<br />
<br />
查建英：对。<br />
<br />
窦文涛：当时大家都叫好，但是胡适反对，但是胡适被人骂，你怎么维护这个封建皇帝。<br />
<br />
吴晓波：对。<br />
<br />
窦文涛：但是胡适当时的理由也是这个，你当时国民政府跟清王室是有协议的，维持它这个待遇，你现在也是不守契约，对你未来的行政有很大的影响，这也是这个道理。<br />
<br />
吴晓波：所以这次反思的其实是一个契约的精神。第二个我认为现在中国的企业家阶层，现在真的要反思，原来大家都是没钱人，没有关系，现在这个阶层已经越来越大了，你按胡润排行榜的数据，中国现在过亿资产的人，有8万多。<br />
<br />
窦文涛：8万多亿万富翁。<br />
<br />
吴晓波：过千万资产的人有八十多万人，你还有很多做中小企业的一堆人，这一堆人十九年以后中国没有出现的过的人，出现了有产阶级，这一波人，他的阶层觉悟是没有的，而且中国历来有一种仇富情绪，《水浒》里面叫杀富济穷，没有理由的，为什么呢？为富不仁嘛。<br />
<br />
窦文涛：对。<br />
<br />
吴晓波：为富不仁是一个先天性的东西，现在我们肯定是财富合法所得，要进行保护私人资产这个问题，慢慢已经开始有了。但是中国现在是一个转型社会等等，所以民间的仇富情绪非常的剧烈，你看2008年王石汶川那个事情，你可以看得出来，一旦一个天灾人祸，首先被谴责的就是那些有钱人。<br />
<br />
窦文涛：避捐的有钱人。<br />
<br />
吴晓波：所以在这个时候，这个阶层的人如果还没有阶层觉悟的话，本身包括我们的理论界，中国也需要像胡适这样的人，来呼唤这些契约的精神，重新来规范，说这个国家里面，有钱人他到底是一个进步角色，还是一个反动角色，是一个被剥夺的角色。我写《跌荡一百年》，写到后来，我就跟大家讲一句话，我就跟大家说，至少我从洋务运动以来，中国搞现代化运动以来，中国的企业家阶层是一个进步阶层，不是应该被关在笼子里的一群人。<br />
<br />
窦文涛：他为祖国强大做贡献。<br />
<br />
查建英：对，很多爱国主义。<br />
<br />
吴晓波：很多爱国者在里面，国家处于困难的时候，他爱国家，处于正常的时候，他繁荣商业，所以中国民间的仇富情绪，一定要慢慢化解掉。但是这个东西，其实你跟民间讲也没用，现在还得跟有钱人讲。<br />
