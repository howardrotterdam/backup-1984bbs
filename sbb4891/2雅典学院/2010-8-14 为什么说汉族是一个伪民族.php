													<h2>为什么说汉族是一个伪民族</h2>
						
						
																											<div id="postmessage_782286" class="t_msgfont">作者：飞虎队<br />
<br />
汉族是一个伪民族，我相信这个观念是我第一个提出来的，但在当时太超前得不到大家认同，相反还受到很多责难。很高兴现在有越来越多的人能够接受我的观点了。<br />
<br />
其实，我提出汉族是一个伪民族的观点（同样的，“中华民族”也是一个伪概念），并不是要诋毁这个群体（实因我也是其中一员），而是就事论事地指出一个事实：因我在中国南北各地所见各地“汉族”实在形貌习俗相差太大，且我更注意到中国不同地方人民其地域歧视族群仇恨之深之烈，使我断然无法再相信这是一个同一胞胎的共祖“民族”。<br />
<br />
实际上，近代以前，中国人并不特别地强调自己的民族性，而是很强调自己的家族性，认为自己跟皇帝，跟国家，跟政府之间的关系，也不过就是主与奴的关系，绝不会自作多情地妄想不相干的人群彼此之间是什么“同胞”，只有跟自己的宗族成员才有基于血缘关系上的彼此需要的义务和权利。<br />
<br />
国民党和共产党来了之后，破坏了中国人传统的家族社会，又不能再拿皇帝这个牌子来统治民众，为了加强统治，于是就阴险地臆造（至少是改造过）出一个所谓的“中华民族”，“汉族”，“炎黄子孙”，“华夏同胞”等等概念，妄图用我们都是具有同一祖先同一血脉的借口来欺骗大家接受他们的统治，并可进而奴役众人。<br />
<br />
虽然中国人内心里实际上根本就没有把这些骗人的鬼话当真，这点只要看看中国人彼此之间的仇恨，斗争，尔虞我诈，互相欺蒙，就可毫无疑问地得到证明。但是，自从这些概念被统治者强加给民众之后，他就成为了一个政治正确性的枷锁，任何人即使内心并不信服认同，但是在实际行动上也不得不用以为自保和害人的工具。<br />
<br />
那么为什么这一套骗术对中国人特别有效呢？因为中国人在过去几千里的家族社会中培养出一种无赖心理：你是我的子女我就可以打你；你是我的父母我就可以吃你；中国人过去是把彼此基于血缘关系上就可为所欲为无穷尽地索取看作是天经地义的。<br />
<br />
所以国民党和共产党很巧妙地利用了中国人的这种心理，狡猾地臆造出一个我们都是“中华民族”“炎黄子孙”“汉族同胞”的同一祖先同一血统的谎言，来作为借口以实现自己对民众具有无穷尽的统治权以及民众具有无穷尽的为奴义务的目的。可以说是相当成功的。因为中国人那种无赖心理决定了他们即使内心里根本不信“我们是同胞”这种鬼话但是在现实生活中他们却很乐意用这个作为工具来整人害人从别人那里索利；但至于别人是不是也会同样用这个作为工具来整他害他盗抢他，他们就不管了，反正今朝有酒今朝醉。<br />
<br />
故此，我认为应该坚决地破除这种“炎黄子孙”“中华民族”“汉族同胞”的迷思，在任何社会生活和国家事务中都绝不能再渲染种族性血缘性等糟粕，而是应该科学地强调和提倡契约性，以公民的身份来处理人与人之间人与国家人与政府之间的关系，如你给予我应有的具有相等政治权利待遇的公民身份，则我认同你们跟我是同一群体，认同这个国家是我的国家，认同这个政府是我的政府；如不，则不管你以任何“同胞”“炎黄子裔”“民族”的鬼话来拉拢诱骗，都统统滚你妈的，我跟你们没有任何关系，对你们没有任何义务，乃至可以彻底反掉你。<br />
<br />
我认为中国人什么时候能具有这样一种胸襟和认识，中国就有希望了，中国就能真正地走上现代化之路。</div>

							
							
							
							
