<br />
&nbsp; &nbsp; “无暇号事件”（指中国渔船撞坏美国反潜声纳浮标）后，美方向中国抗议。外交部要求南海渔政局进京汇报，解释情况。南海渔政称，主管部门是农业部，进京向农业部汇报。农业部听取汇报后，说南海渔政做得对，同时通报外交部，请他们派人来听情况。外交部开始说派个司长，后又说是副司长，最后来了个处长，听了情况后，也说南海渔政做得对。外交部对农业部说，以后采取类似行动，要及时向我们通报。农业部称，都是国务院下属部门，而且农业部由副总理分管，外交部由国务委员分管，不存在通报的问题，以后要及时向我们了解相关情况。<br />
<br />
&nbsp; &nbsp; 基辛格是过气的政治人物，毫无影响力，但每次来都要当国宾接待，指望他能够影响美国战略决策人物的思维，根本没有作用。<br />
&nbsp; &nbsp; 只有中国人有“中国人民的老朋友”这个说法。基辛格、布热津斯基这一类的“中国人民的老朋友”，先不说他们有没有用，他们是不是真的朋友也还两说。<br />
&nbsp; &nbsp; 尼克松是“中国人民的老朋友”。2001年解秘了1972年尼克松访华途中在夏威夷所写的笔记，提醒自己如何与毛会谈：<br />
&nbsp; &nbsp; Treat him（as Emperor）<br />
&nbsp; &nbsp; 1、 Don`t quarrel.<br />
&nbsp; &nbsp; 2、 Don`t praise him（too much）.<br />
&nbsp; &nbsp; 3、 praise the people-art，ancient<br />
&nbsp; &nbsp; 4、 praise poems.<br />
&nbsp; &nbsp; 5、 Love of county.<br />
&nbsp; &nbsp; 尼克松在反面写下的访华目的：Viet Nam、Soviet Union.<br />
<br />
<br />
&nbsp; &nbsp; 中国历史上一贯是南粮北运，现在已经变成北粮南运。北方缺水，种一季麦子要浇四遍水，于是再搞南水北调。战略考虑明显有问题。<br />
&nbsp; &nbsp; 全国每年消耗粮食5万亿斤，但国际市场一年粮食交易量只有2.5万亿斤。农村工作领导小组负责人到芝加哥粮食交易所访问，想通过交易大厅的大玻璃去看一看交易现场。董事长脸色发白――如果中国人出现在交易大厅的玻璃后面，世界粮价马上飙涨。<br />
<br />
&nbsp; &nbsp; 1984年10月，中英关于香港问题的谈判陷入僵局，中方因为此前一步不让，此时打算在驻军问题上让步，可以驻军深圳。邓小平在会见香港人士时，非常激动地表示一定要在香港驻军，除了驻军还有什么能体现中国对香港的主权？别人（军委、外交部）说话都不算，只有我和他（姬鹏飞）算数。指示军队要准备武力接管，出了意外要能“一锤子砸死”。军队开始城市攻防作战的研究。<br />
&nbsp; &nbsp; 后来的中英谈判中，中方有人投敌，把谈判底线、回归时江核心的行程全部交给英方，但英方也因此知道了武力接收计划。香港回归交接十分顺利。<br />
&nbsp; &nbsp; 2003年7月1日上午，温相访港结束。下午，香港50万人散步，反对为《基本法》23条启动立法。2004年7月1日，30万人散步，要求加快政改，要求普选。但中央对此并不十分担心，因为有驻军。<br />
<br />
&nbsp; &nbsp; 2005年，中印联合声明中，中方承认“印度共和国的锡金邦”，换来的仅是印度在声明中重申不支持藏独（这是双方建交的基础），不允许任何人在印度境内从事分裂中国的活动（达兰萨拉的藏独活动从未得到印度的制止）。而印度媒体对此并不领情，仅称注意到了中方在提法上的变化。<br />
&nbsp; &nbsp; 从亚东到乃堆拉山口的中锡边境，从此变成中印边境锡金段。阿三将锡金士兵全部撤下，换成印度士兵。一条原本和平的边境线，变得麻烦不断。2009年7、8、9年，媒体不断报道双方边防军人暴发冲突，造成伤亡。伤亡是谣言，但冲突不断的就是中印边境锡金段。印度在边境线中方一侧修筑工事，中方多次进行捣毁。<br />
&nbsp; &nbsp; 乃堆拉山口，印度树起大幅标语：伟大的印度国万岁！我们必胜！中方的标语是：人在阵地在！誓与阵地共存亡！<br />
<br />
&nbsp; &nbsp; 现在更大的问题是要防止不丹问题锡金化。在中方承认印度对锡金的吞并后，印度一个团已经进驻不丹。原本友善的不丹士兵也变得敌对起来。不丹边防军去年开枪打死了4名越境的边民，中方要求不丹为此赔偿每人40万元，对方强硬回绝，只肯每人赔偿2万元。<br />
&nbsp; &nbsp; 承认印度吞并锡金，使中方在藏南9万平方公里的被占领土谈判时，凭空少了一张筹码。<br />
<br />
&nbsp; &nbsp; 我们现在心理过于脆弱，怕出事，怕死人。3.14事件中，西藏的公安、武警、军队表现得过于软弱。武警在7.5事件中表现比3.14要好。<br />
<br />
&nbsp; &nbsp; 7.5事件中影响最坏的不是7.5，而是7.7和9.3。和平居民对政府失去信心。<br />
<br />
&nbsp; &nbsp; 事实证明，花钱买不到平安，依靠发展无法解决所有问题。必须大力推进民族融合、推进宗教现代化和世俗化。<br />
<br />
&nbsp; &nbsp; 某些强化民族属性、不利民族融合的作法必须改变。藏疆二个座谈会开过会后，已明确要强制普及双语教育。<br />
<br />
&nbsp; &nbsp; 文革末期是推进藏传佛教世俗化的大好机会，但是丧失了。现在藏传佛教最大的资助者，不是达赖，不是中情局，而是汉族富商。<br />
<br />
&nbsp; &nbsp; 某州委书记上任时放话，他到寺庙里去，庙里的活佛必须出门迎接，给他献哈达。这是为了树立世俗政权的权威。当然作为礼貌，他也会还礼。<br />
&nbsp; &nbsp; 但是作为书记的上级，那些人见到活佛都诚惶诚恐，低声下气，于是活佛怕书记、书记怕上级、上级怕活佛。当政者没有真正的信仰。<br />
<br />
&nbsp; &nbsp; 1957年，18军驻藏兵力由3万撤到1.7万，于是1959年达赖集团暴发叛乱。<br />
&nbsp; &nbsp; 1982年，胡耀邦提出西藏工作三条意见：开放、免税、走人。第三条大错，先后走了20万万基层扎根的干部，损失巨大。现在援藏干部的三年任期，都是咬牙坚持完就走人，无法扎根，和当年18军在当地扎根无法同日而语（18军参谋长向军长张国华提出，是不是建议中央，驻军实行轮换，张国华大骂，不想干可以滚！）。<br />
