													<h2>曹长青：做中共间谍下场最惨</h2>
						
						
																											<div id="postmessage_760695" class="t_msgfont">最近美国抓获十名俄国间谍，成为引人注目的国际新闻，因这是冷战结束以来，美国破获人数最多的外国间谍案。<br />
<br />
从媒体上看报导，简直像看冷战时代的间谍片，情节大同小异，“假护照、购买死人的身份、隐形墨水书写情报、在熙熙攘攘的地铁楼梯上交换情报。”美国中央情报局的起诉书上就这样描绘。被抓获的十名俄国间谍，四对是假扮夫妇，在美国生活了多年。电影中的间谍“假扮夫妇”可以理解，但在现实中，两个并不相爱的人，居然生活在一个屋檐下，还得像“夫妻”那样过活，那种日子，也许只有那些做间谍的“特殊材料”才能过。<br />
<br />
美国反间谍机构，早就发现这些嫌犯，对他们监控已有七年。最近则拿到确凿证据：去年美国总统奥巴马访俄罗斯时，莫斯科向这些间谍发出指令，要他们收集美国在削减战略武器条约上的立场，对伊朗核计划的态度，以及随同奥巴马出访官员的背景等。<br />
<br />
美国文化重视营救战俘<br />
<br />
这些间谍的人生命运，像是洗“三温暖”，突然从热池子掉到冷水里，原来是有钱有车甚至有浪漫的现实版007，现在则一下子变成囚犯，按美国法律，间谍罪就可判五年，另外他们的洗钱罪名成立的话，最高面临二十年刑期，等于这辈子完了。<br />
<br />
但他们也真是“庆幸”，因是在美国被抓获，美国人要用他们交换被俄方抓获的四名美国间谍。用十个人换四个，这本身像是赔本买卖，但这就是美国，哪怕换一个人，也会这么干，因为美国文化，非常重视营救战俘，珍惜生命。于是，一场冷战以来最戏剧化的交换间谍场面，在欧洲“上演”：两架飞机，各自载着对方间谍，降落在维也纳机场，然后十换四。<br />
<br />
十名俄国间谍，全是俄国人。而被俄方抓获的四名美国间谍，也都是俄国人，其中三人还是俄国情报系统的上校军官。从这种身份不同也可猜测说，这四个“美国间谍”可能获得的情报质量更高。<br />
<br />
但俄国人愿意做这个“买卖”，因为俄国民主了，也看重人的生命和价值。媒体报导说，被交换回的俄国间谍，俄当局提供每月两千美元的生活费，并报销其子女前往莫斯科探亲的费用。这十名俄国人在美关押期间，俄国外交官曾多次前往监狱探望。他们回国后，总理普京还亲去探望，称赞他们为国家作出贡献，共唱民族歌曲。<br />
<br />
十四名美俄间谍，各自回到自己服务的国家，获得自由。这种“交换间谍”，可能会让“中国间谍们”羡慕万分，因为在美国，无论多少中国间谍被抓，中共当局从来都不予承认，更不要说像美国，甚至俄国这样，交换间谍，把自己的人要回来。在人类间谍史上，中国间谍的命运，可能是最悲惨的。<br />
<br />
中国间谍“实在太厉害”<br />
<br />
中国在美国的间谍活动，一点也不比俄国逊色。前年美国《外交政策》杂志刊登文章说，中共情报部门“中国国家安全部”在全球最具实力的六大间谍机构中排名第二。去年美国联邦调查局长穆勒在国会作证时说，中国间谍“实在太厉害”。联邦众议员兰迪&#8231;福布斯说，“中国现在已经构成美国最大的间谍威胁”。根据美国司法部向《华盛顿邮报》“谍语”专栏提供的最新备忘录，只是从2008年至今，美国司法部起诉的中国间谍案就有26个，其中44人被判有罪，在美国监狱服刑。<br />
<br />
对于美方的说法，中共外交部一向否定，其发言人说，“我们反覆重申，所谓偷窃美国军事机密的说法子虚乌有，所有的指责都别有目的。”中共当局一如既往，从来不承认派间谍到美国。当然，对被美方抓获的自己的间谍，也就不闻不问。<br />
<br />
例如，美方抓获的最著名中共间谍，是曾潜伏在中央情报局达三十年之久的金无怠（Larry Wu Tai Chin），他曾把美国很多最高保密等级的文件偷拍成微型胶卷，秘密交给北京方面。后来是中共国安部官员俞强声（他弟弟俞正声现任中共上海市委书记）叛逃到西方，才使金被捕。<br />
北京特工杀人灭口<br />
<br />
金无怠间谍案在美国轰动一时，因是公开审理。但北京当局始终不承认金是他们的特工。金在等待判决期间还呼吁北京，拿魏京生（当时在押）作筹码交换他出狱。但北京方面不仅没同意，当时的中共外交部发言人李肇星（后做外长）还公开否认跟金有任何关系。根据起诉的罪名，金无怠“将面对最高两次终生监禁和另外八十三年徒刑”，最后金无怠绝望至极，在囚室内用塑料袋包头，用鞋带勒紧，窒息而死。<br />
<br />
在金无怠自杀前，曾任纽约《自由时报》总编的华文记者陈国坤曾去监狱采访过他，这位来自台湾的记者对我描述过当时金无怠的绝望之情。但对金无怠的自杀，一直有争议，因用塑料袋套头窒息而死，操作上不易做到。后来金的遗孀周谨予出版《我的丈夫金无怠之死》一书，提出可能是北京特工为灭口而杀掉了金无怠。<br />
<br />
从金无怠案至今，美方抓获了很多中共间谍，无论是去年被美方破获的两起“中国间谍”案中的郭台生等三人，还是更早一点的洛杉矶的情色间谍陈文英，更有今年春在波士顿被抓获的经济间谍钟东蕃（窃藏美国航天情报，被判15年）、麦大志（Chi Mak，把美国防技术出口给中国，已判24年）等，中共当局都一如既往，从来都不承认他们是自己的间谍，当然就更无可能，像美俄那样交换间谍，让自己的特工获得自由。所以那些为中共做间谍的人，只有为党国献身这一条选择，要么是望眼欲穿，把美国的牢底坐穿，要么像金无怠那样有胆量自杀。不过今天在美国监狱再能找到塑料袋套头，可能也难了；而且中共派人来杀人灭口，也更不容易了。所以中共间谍们要死，都会死得“不明不白”。@<br />
