　　一个时期以来，摸摸党在各条舆论战线上战果辉煌，引起了全社会的强烈反弹。我的功劳是主要的，党内其他同志是次要的。大家为了追求真理，进行了热烈的争论，很多议论都是针对我的，我也听到了。我认为，这是民主的良好开端，鉴定完毕。但有一种党外的意见，我不能不回应，因为这关系着我的颜面，而我的颜面就是党的未来。很多人说我见啥批啥、为反对而反对、破坏民主阵线，这没什么，都是事实。但最后还给我安了一条罪名，说我没有政治观点，这可就不妥了。没有观点，怎么能批驳人呢。没有观点，怎么可能破坏什么阵线呢。没有观点的人都当上了人大代表，怎么可能在牛博混呢。看来党外很多人缺乏领悟力，要我点拨一下。好吧，我给大家交底。<br />
<br />
　　我不知道在座的知不知道国内有过一段时期，自由主义者和新左派大混战。论战结束，但文章留下了，它们其实就是我最早了解政治的窗口。朱学勤和徐友渔，虽然长的不帅，但我记得他们。接着读王小波，虽然有老婆，还是恋上了。再往后，我连余杰我都看，捂着鼻子看的，终于明白了什么叫山寨自由主义。但总的来说，我觉得头脑清楚了。这段启蒙岁月告诉我什么呢？要争论，要吵架，要掐人。互相抨击最容易暴露问题，谁赢谁输不重要，每个人的观点亮出来了。争论看起来也有趣，就像观赏体育比赛，寓教于乐。但这几年，这类争论就少了，大家都走中间路线，搞调和油，搞鸡尾酒。明明是相互冲突的观念，结果搅在了一起。最可笑的是，某些人还说当年的左右争论没必要，让党看笑话，鹬蚌相争，渔翁得利。可在我看来，如果没有这场争论，恐怕今天真正关心政治的人会更少。我就是这样的，是左右之争启蒙了我。我从中发现，政治可以不是体力活儿，而是脑力活儿。<br />
<br />
　　看看今天的言论竞技场，几乎空无一人了。那些自由主义者都去哪里了？新左派都以自由派自居，妄称自己已经平息了争论。除了在政府机关和大学，他们还驻扎在南方报业凤凰卫视牛博等媒体上，将左翼思想包装成右翼观念来推销。相应地，官方也在采取更强硬的手段打压言论。渐渐地，大家都开始用一个腔调说话，没精打采地喊两句民主自由，以为这就建立了“共同底线”。这个时代的政治氛围已经退化到何等地步。当我们用文字时，政治就是理性的说服。我们要拼的就是观念，是左派是右派，一目了然，不用遮遮掩掩、藏着掖着。如果认为政治是行动的，就直接去搞组织，搞暴力，根本用不着事先张扬。言论场上的政治，拼的不是流血流汗，而是智识。我不知道有多少人能理解这一点，当人与人之间分歧加大，两边拉扯，争执不下的时候，这才需要制定一个程序去解决。只有这样才可能有民主――“自由的民主”。已经有了“共识”就不需要“民主”，没有“左右之争”就形同“一党专政”。<br />
<br />
　　对于政治，我是靠心理感应的，但一向很准。我读书杂，不系统，但从中总领悟到真理。有些闲书里的轶闻给我的启发更大，远胜于理论书籍。我印象很深的有这么几个。东欧天鹅绒革命成功后，流亡诗人米沃什回到波兰，参加了一个诗人的聚会。按理说，米沃什应该受到欢迎，结果会上他受到年轻一代诗人的围攻。他们指责他曾经是马克思主义者，米沃什委屈地为自己辩护，那是历史的局限，那个年代谁不是呢。这个故事就把我震了，要知道文青都是左派脑袋，诗人竟然这么群情激愤地清算马克思主义，太不可思议了。回过头看看我们这儿，连教西方经济学的老师都没这等知识勇气，他们还在为社会主义梳妆打扮，为马克思主义修甲隆胸。还有一个故事，米奇尼克去拜访捷克总统哈维尔，整个谈话过程中经常被哈维尔的秘书打断，这位秘书不仅当面讽刺米奇尼克，还忍不住纠正哈维尔的错误。秘书如此失态，顾不上礼节，只是因为米奇尼克越来越左倾。后来，我又读了一些有关东欧的文章，发现他们很多知识分子将“布拉格之春”称之为“自由主义运动”，这个措辞就和我们的习惯不一样。我们喜欢把自己喜欢的群众运动叫做“民主运动”。难怪89年苏东没流血就成功了，中国流那么多血却失败了，区别就在这里！他们要自由，我们要民主。他们有太多自由主义者，我们有太多民主主义者。我还联想到台湾，他们的有志之士喊出的口号是“自由中国”，他们不是民主控，但他们得到了民主。<br />
<br />
　　这样的故事里蕴含的政治启示，对我来说就已经够了。我明白到，如果不在观念上和恶魔决裂，就永远战胜不了他。只有你成为了和对手不一样的人，才可能战胜他。这种战胜，当然不是消灭他，而是让他和你妥协。他是集体主义者，你就要成为个人主义者。他鼓吹社会主义，你就要赞美资本主义。他捍卫马克思主义，你就要弘扬市场原教旨。这难道不是抗争吗？难道游行示威才是抗争吗？难道跳楼自焚才是抗争？当我告诉土共，我恶心你们的价值观，恶心你们信奉的主义，这还不是抗争？里根总统说过，将马列主义扫进垃圾堆吧，让那些被奴役的人得到自由。如此鲜明的政治立场，还不够感召力，不够清晰动人吗？最可笑的是那些“形右实左”的愤愤，打着反共的旗帜，宣扬的是土共的意识形态：全世界无产者联合起来，劫富济贫阶级斗争，社会主义道路万岁。台上道貌岸然，台下大耍流氓腔：我要用最恶毒最黄色的话讽刺你。这是抗争吗？这只是反对主子而已，不是反对专制。这样的民主运动，送走了瘟神，迎来的还是瘟神。我还要多提醒一句，新暴君永远比老暴君更贪婪，因为长久压抑，极度饥渴。<br />
<br />
　　政治就是要力争。没有观念的话，又如何知道自己为何而争？一场反政府示威，如果是毛左组织的，我就不会参与，因为他们的主张是我反对的。我也不可能参加轮子功的行动，他们是我的敌人的敌人，却不是我的朋友。要政治有所行动，就要有政治观念，有政治观念，又怎么可能不分左右呢？我批评秦晖，很多人误以为我批评他不肯签署宪章。非也非也，我批评的是他吹嘘“共同底线”，这种荒唐的主张连他自己都不愿作秀，居然还有那么多人当成政治信条。在这面浑水摸鱼的旗帜下，一群虚伪且无脑的人聚集在了一起，以不争论之名，表演起自己的中庸、平和和客观来了。他们专门负责纠察：你偏激了，你破坏阵营了，你让亲者痛仇者快了。当然，和稀泥也是一种政治技巧，但这已经落伍啦。这种手段的失败集中体现在那些围绕土共的民主党派身上，他们一会儿被打，一会儿被摸，被整治的服服帖帖。这样的中间道路还不够吗？在官方已经有了一个，难道民间还要复制一个吗？<br />
<br />
　　我的政治信条是什么？“四个至上”，个人至上、财产至上、法治至上、市场至上。也可以这么表述：个人免于政府强制，财产免于政府分配，法治免于政府立法，市场免于政府干预。明眼人应该看得出来，在政治的光谱里，这是最右的。这也是一条最孤独的道路，因为在现实政治的妥协中，最左和最右的都会被牺牲掉，最终达成妥协的肯定就是中间派。目前看来，土共是中左，民主派是中右。一旦左右两端拉扯的厉害，他们都腹背受敌，受到左右夹击，就有可能摒弃前嫌，坐在谈判桌上。这个时候，民主就可能提到议程。眼下中国的问题是，靠左的太多，而靠右的太少。左的太极端，右的太靠中间。这些的局面太和谐了，没有两头的张力，土共永远感觉不到受压。于是，在这一个伟大的历史时刻，摸摸党诞生了，开辟了上了一条特殊的政治道路。在右边使劲拉扯，为民主控们腾挪空间，让他们和土共因为有了共同的敌人而愿意和解，勾肩搭背、眉来眼去。这就是我们的使命，为了自由的民主而牺牲了自己的政治前途。大部分民主控不会理解我们的良苦用心，他们还会继续攻击我们。但不要紧，我们是伟大的党，忍辱负重的党，真正理想主义的党，让暴风雨来的更猛烈些吧！我们恶搞，我们胡闹，我们勇往直前！为了自由，羞辱民主控，解放全中国！</div>

							
							
							
							
