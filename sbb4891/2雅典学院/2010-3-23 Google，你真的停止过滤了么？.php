<br />
2、在应为google设置关闭安全搜索功能，首先重新打开google这个时候首页已经是英文的了，进入右上角“Search settings”，就会发现“SafeSearch Filtering ”，这个选项有三个选项，我们选择Do not filter my search results这个选项，然后保存设置。<br />
<br />
