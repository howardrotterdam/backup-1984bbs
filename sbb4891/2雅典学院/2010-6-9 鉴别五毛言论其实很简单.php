													<h2>鉴别五毛言论其实很简单</h2>
						
						
																											<div id="postmessage_668406" class="t_msgfont">拒绝五毛言论，主要问题就是判断什么是五毛言论。论坛的属性与特点就是只属意言论，而不必关注人身属性。有不少人出于各种各样的原因，纠结于别人如何判断谁是五毛的问题，颇有些“寡人有疾”式的弓杯蛇影，揣测不安啊。见到别人说五毛就浑身不自在，喋喋不休地混淆各种事物区别或是非黑白标准，大有不把透视五毛之水搅浑不罢休的味道。有一句网络俗话说得好：屏幕后面，谁知道你是一条狗？其深层次的涵义就是，你只要说人话，你是狗，还是人，谁管你呢！<br />
我个人认为，论坛拒绝不了五毛和共匪党舆论特务，但是应当也能够拒绝五毛言论。如何鉴别五毛言论呢？鉴别原则是宁纵勿枉，重点打击气焰嚣张的反文明反人类语言；鉴别标准其实很简单，一是明显故意歪曲历史事实与现实真相的，就是五毛语言，二是反民主、反自由、反文明、反人类等反动价值观的言论就是五毛言论。比如说“狗屎可以作为人类食粮”这样的话，就是反文明价值观的语言；比如说“中国坚决不能搞三权分立”这样的话，就是反民主反自由价值观的语言。<br />
<br />
对五毛语言，我所指的拒绝与打击是，让这样的语言一出来就遭到痛斥，遭到围观，受到成堆的砖头海扁，使其受到论坛规则层面上的羞辱待遇。<br />
<br />
你五毛不是一边支持着压迫言论自由的制度，一边又口口声声向我要他所谓的言论权利么？那你是有很多选择的，一是不要逼脸、不顾羞耻，不折不扣地继续完成上级交给你的语言任务，二是变换花样，修饰、修整赤裸裸的党棍语言，把狗屎变成臭豆腐卖给论坛上的人吃；三是表达你自己真正真实的思想想法，至少不要编造一些类似“我是下岗工人，创业成功开办一个年盈利百万的公司”间谍身份谎言，诸如此类，等等等等。所有人的所有言论，不论是专制极权境内，还是民主宪政国家，都是要承担其相应后果的。就如同放屁自由一样，你五毛如果在你党妈妈的饭桌上放屁，也是要遭白眼的。<br />
<br />
如果有人硬是要揣着明白装糊涂，或智商低下，纠结于语言表达中的边缘的极端的微妙的是非、曲直或真伪情况，硬是要强调无法鉴别之类的不可知论的话，我就请他在夏天用屁当风乘凉，请他把狗屎做臭豆腐下饭，教他一句成语什么叫“泾渭分明”了，可否？</div>

							
							
							
							
