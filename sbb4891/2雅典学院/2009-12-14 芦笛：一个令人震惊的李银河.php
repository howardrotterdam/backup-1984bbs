													<h2>芦笛：一个令人震惊的李银河</h2>
						
						
																											<div id="postmessage_372282" class="t_msgfont">一个令人震惊的李银河 <br />
芦笛 <br />
<br />
刚才在《凯迪&#8226;猫眼看人》上看到李银河的博文《一部令人震惊的电影》，倍感震惊，原来这竟然能是一位留美博士说出来的话： <br />
<br />
“贫穷是罪恶。早就听到过这种说法，原来很不理解，因为共产党的意识形态一直是表扬贫穷的。其实，贫穷有什么可表扬的呢？在几千年弱肉强食的历史中，贫穷不一直就是罪恶吗？贫穷就是在残酷的生存竞争中败下阵来。失败有什么可表扬的呢？贫穷不是美的，是丑的；不是值得骄傲的，是值得羞愧的。或者是比较懒惰，或者是比较笨，或者是不够幸运。如果一个人活成了下层人，那就是活得最失败的人，被淘汰的人，有什么可表扬的呢？” <br />
<br />
道义勿论，这段话暴露出来的头脑超级混乱，我只在王小波的同窗刘晓阳的烂文章里见到过。李银河博士为何也会与刘晓阳同病？据说李博士当年留学美国是修社会学的，我可做梦也没想到美国的社会学教育水平竟然会如此低下，以致一个头脑极度混乱、连起码思维能力都欠奉的同志都还能混到学位，哪怕是三流大学也不该啊？ <br />
<br />
李博士的意思很简单，只是她没有足够智能把那话说明白（或没敢说明白，待考），她想说的其实是“穷人是罪人”，为什么？因为贫穷是罪恶。生活于罪恶之中的人当然只能是罪人。为什么贫穷是罪恶呢？因为“生存竞争，优胜劣败”，“贫穷就是在残酷的生存竞争中败下阵来”，穷人“或者是比较懒惰，或者是比较笨，或者是不够幸运。如果一个人活成了下层人，那就是活得最失败的人，被淘汰的人，有什么可表扬的呢？” <br />
<br />
就算是如此吧，那失败就是罪恶，失败者就是罪人么？就算不值得表扬，似乎也不需要李博士出来大义凛然地打成罪人吧？难道人家受穷还不够，还需要您这位海龟高知痛打落水狗？敢情您当初在美国学的，就是这套社会达尔文主义？我还以为那是纳粹的官方哲学呢。无怪您对尼采无比激赏，果然是纳粹的隔代知音。只是中国当年也曾被列强痛打过，那是不是也是先进膺惩落后、正义荡涤罪恶？ <br />
<br />
而且，想当年王小波也好，刘晓阳也好，家里都是穷棒子，或至少参加了穷棒子的暴动，那是不是也是失败者抵抗优胜者的大规模犯罪行为？王小波当年负笈海外时穷到活不下去，欠债累累，是否也该遭受您义正词严的谴责：“你们都该死，你们不配活在这个世界上。”以便让我们每个人听到后都“有一种强烈的快感，欢呼雀跃，觉得解气之极”？ <br />
<br />
这难道是人，一个女人，而况是一个曾负笈海外、饱受异国人文熏陶的博士说得出来的话？ <br />
<br />
这里的论证之荒诞不经，大概用不着我再指出了――哪怕连高中生也能看出“失败＝罪恶”、“失败者＝不配活在这个世界上的罪人”的荒唐，那原是刘晓阳的专利，不意竟为李银河女士学去，倒也难得。 <br />
<br />
最令人震惊的是，李博士这惊人的社会学结论，竟然是从一部电影中得出的！若那是部纪实的文献片，都仍然是一个学者的耻辱――一个小镇能有什么统计意义，岂能据之得出涉及全人类的如此严重的社会学结论来？何况那整个结论竟然建立在一部虚构的故事片上！妈妈米啊，您能想象世上有这种，呃，这种，喔，这种，嗯，这种“学者”么？ <br />
<br />
我看就凭这点贫弱的智力，李博士也不配`颜自称优胜者，要么那“优胜”来得不干不净，与其智能绝对成负相关。 <br />
<br />
这就是症结所在：李博士的新版“狼羊律”（这是某位网友揭示的“宇宙运行规律”。他说，富人和穷人的关系，乃是狼与羊的关系，狼吃羊是为了防止羊过度繁殖，打破生态平衡，造成生态危机，因此其实是对羊的恩惠）忽略了一个至关重要的问题：生存竞争是不是只有一种，都是公平合理的？优胜是否都是光荣？失败是否都意味着耻辱乃至罪恶？ <br />
<br />
但凡智力正常者，只要有过国内外生活的经历，哪怕头让碓打过（或被门夹过，either way or any other way），也该意识到这世界上的生存竞争并不一样：有良性竞争，那就是社会成员竞相比赛创造力；有恶性竞争，那就是社会成员竞相比赛控制和伤害他人的能力。前者强调的是机会均等、费厄泼赖，严禁权力（亦即隐藏的暴力）介入逐利活动，而后者则利用权力落差制造出来的“机会梯度”，以“合法伤害权”作为最雄厚的资本，空手套白狼，化为巨额财富。其最杰出的代表，便是前人大副委员长成克杰、前北京市长陈希同、前上海市长陈良宇，等等。这些人从事的当然也是剧烈的生存竞争，也曾一度优胜过，莫非那也是一种光荣？当然，他们最后失败了，但一度拥有过光荣似乎也是事实。那么，这些人是否可以视为项羽式的悲剧英雄？ <br />
<br />
所以，尼采那句名言当然没错，只是李博士没有足够的智能理解罢了。贫穷是社会病态的表现，确实是罪恶，是耻辱，但它是全社会的耻辱尤其是统治集团的耻辱，首先是统治集团犯下的罪恶。这就是肯尼迪总统和约翰逊总统为何要向贫穷宣战，而尼克松总统为何听到美国竟然还有人饿死后会无限震惊。一个常态国家的政府，应该致力于消除社会弊病，以免造成“不公正的贫穷”，亦即未能确保公平竞争而导致“不合理的失败者”产生，更不能蓄意制造“低人权优势”那种“国富民穷，国进民退”的“必要的贫穷”。如果这么做，那才是犯罪，才是耻辱。把这些蓄意制造贫穷的罪犯杀光了，人民大众才会“有一种强烈的快感，欢呼雀跃，觉得解气之极”。 <br />
<br />
当然，李博士之所以堕为社会达尔文主义者，引纳粹为同调，也不是毫无原因的。她自己也说了，那其实是官方“民粹主义”媚俗宣传引出来的反弹。在这点上，她又显示了智能发育的惊人迟缓与偏枯。 <br />
<br />
