　　 社会主义好还是资本主义好。从社会正义的角度讲，还是社会主义好。社会主义就是救济穷人，资本主义就是带来恶性的两极分化，富人愈富，穷人愈穷。所以像尼采哲学那样不同情穷人，不帮助穷人，是不正义的。社会主义就要大搞“杀富济贫”，当然是经济上的杀，不是肉体上的杀，就是要把从富人手里拿过来的钱为穷人搞福利，保障大家都有一个温饱体面的生活，不能“朱门酒肉臭，路有冻死骨”。中国的基尼系数从70年代的0.2飙升至0.5，从全世界最平等的社会成为贫富差异最大的社会，是一个大问题，搞不好要出大乱子。 <br />
　　 <br />
　　 要自由还是要平等。人生而不平等，有人长得美，有人长得丑；有人聪明，有人愚笨；有人生在富人家，有人生在穷人家。如果只强调自由和公平标准，由于起点不同，社会就可能失去平等和正义。在这一点上自由主义与尼采有相通之处，都强调自由，忽略平等。这也是我以自由主义的立场会掉进尼采陷阱的原因。尼采的哲学适用于丛林社会，弱肉强食，适者生存，只有公平，没有正义。就像《狗镇》里的社会，谁强谁就出头，谁弱谁就活该倒霉。如果这个世界永远是个丛林，那可真成了人间地狱了。 <br />
　　 <br />
　　 一位终身怀抱理想主义的朋友设想过一个乌托邦的社会，在其中所有的人都有一份基本的生活保障，其余愿意从事高级的具有创造性的工作的人可以仅凭自愿和兴趣去做，并不一定要高额报酬。因为他对做这样的事情有兴趣（比如写小说，画画，作曲，跳舞），你给不给报酬他都会乐在其中。我没有认真对待这个设想，觉得太不可能，太离谱。可是我不能不承认，这是一个非常有创意的、非常大胆的想法。虽然历史上没有成功的先例，但是可以成为激励我们改良社会的一个遥远目标。我觉得它很像马克思所设想的共产主义社会。当一个社会的财富积累到比较高的程度时，这未必不是一个社会改良的切实可行的方案。我强调社会财富积累这个硬指标是因为，贫穷的共产主义方案我们已经实验过了，失败了。 <br />
　　 <br />
――――――――――确实后来博删了。我觉得这文写于观狗镇之后的激动情绪中，完全可以理解。片子我慕名观摩了一下，奇怪怎么以前没听说过</div>

							
							
							
							
