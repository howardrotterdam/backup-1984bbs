													<h2>余世存：王康其人</h2>
						
						
																											<div id="postmessage_376912" class="t_msgfont">余世存：王康其人<br />
<br />
<br />
一。　王康是说不尽的。 <br />
<br />
2001年5月，北京当代汉语研究所学术委员会公告了首届当代汉语贡献奖，王康是首届得主之一。学术委员会公布的授奖辞是：王康先生怀抱理想主义，他以布衣之身忧国忧民，对于俄罗斯民族的启示，对于中国的统一前景的展望，在小范围内流传，影响了年轻一代学人。 <br />
尽管首届当代汉语贡献奖的授奖辞较之后来显得简单，但对王康的颁奖仍透露了足够多的内容：王康是一个小众范围内的汉语人格，他的生存之道首先是影响周围，成全自身，进而推动他人生命的自我完善；王康又是一个关怀悠远的中国布衣，他的存在直接汉语的历史、世界的当下经验，并有着极为人性的愿景。在王康那里，连接了汉语世界的历史和未来。 <br />
确实，在相当大的程度上，就积极健康的一面而言，王康是传统文化在当代的人格象征。 <br />
<br />
二。　传统文化在转型的当代中国已被割裂。对大多数生长在大陆中国的同胞来说，他们身受这种割裂而不自知，他们站在生存和一种叫做真理的碎片上，彼此对立、割伤、侮辱或损害。少数先知般的圣贤，几乎是无望地守望着道为天下裂的汉语世界。 <br />
<br />
1949年，经过四年内战，兄弟般合作两次、共存了28年之久的国共两党强弱易势，并修改了自处并共处的依存法则；国民党训政的国民政府成为弱者，弱者几无葬身之地，最后栖身于海岛，强者则统一了中国大陆，建立了中华人民共和国。传统中国文化信仰的阴阳生成之道、传统中国王朝奉行的忠奸名实对立统一之平衡、传统中国人遵从的因果逻辑，在国共两党争霸的过程中，丧失殆尽。转型年代的中国人信奉了一个党、一个领袖、一种强势的顺势势利思维。 <br />
1949年，王康出生于重庆。王父曾在四川大学物理学读书，因爱情转入化学系，天性超脱，习自然科学，对中国式的政治了无兴趣，毕业后做了一个小职员。国共易势之后，前政权的小职员也成为另类。1950年，王康的父亲被捕入狱，罪名似乎是在川大读书时跟踪某地下党员同学，1957年出狱。1982年，父亲在滩子口木材加工厂“退休”后，其工龄仅有四年，问题也得以“改正”。其档案中历史反革命罪状竟无任何证据。所属单位政工人员说，可能是弄错了。王康后来回忆说他父亲在临终前，“他慢慢抓住我的手，好半天才断断续续地说，人是最不好的动物，最不好的动物也比人好。吸了两口水，艰难地咽下，又说，生为一个中国人，实在太惨了。余无所思，但求早死，这是他最后的念头。” <br />
王康的母亲研习化学，新中国成立后，她注定只能做一个普通的中学化学教师，“像所有中国女性一样，作为女儿、妹妹、姐姐、妻子、母亲、祖母和曾祖母以及姨妈、舅妈、儿媳、弟媳、岳母……，母亲在中国发明的所有加诸女性的角色中，都拼命地尽她的心，她的力，她的道。”有着极好国学修养的王母在悲苦的一生中写下了一百多万字的日记，当她的兄长、中共元帅陈毅的同窗唐君毅拒不回大陆，最后在香港去世时，她为早已名闻中外的大哲学家兄长写下了感人的短诗―― <br />
悼 兄 <br />
一、一树五枝，顶枝摧折，四枝彷徨，何所仰息。 <br />
二、天地胡不仁，以人为刍狗，既生我良兄，何忍又夺走。 <br />
三、昔日梦里惊，醒来暗庆幸。今日梦里惊，醒来泪湿衾。 <br />
落月满屋梁，音容何处寻，追思成往事，不觉泪纵横。 <br />
这样的家庭给予了王康足够的屈辱和悲情。以至于在父母相隔不到半年先后谢世之后，王康感慨说：性灵中国、悲情中国、道义中国正在解体，中国老一代知识人正在彻底离开。对这个时代，他们两手干净，两眼清明，灵魂高洁。他们是这个“大时代”最无辜的苦难承受人，罪恶见证人。他们以最大的忍耐和最高的善意与这最荒唐的人生诀别时，后来人能体验其中滋味于万一吗？ <br />
这样的家庭也给予了王康足够的灵性、道义和才思。大舅唐君毅去世后，王康开始接触港台“新儒家”。1978年，王康考入西南师范学院中文系，他在西师组建了一个哲学小组。小组虽然没有坚持下去，但王康个人已经深信不疑，他们正在上天注目之中，秉承天意，践行天意：1949年以来的中国，是近代以来中、外若干历史事件交叉作用的结果，并非中国常态，更非中国终极形态；中国必将再次回到它的传统和大道去寻找存在的连续性和动源。 <br />
这样的王康当然是被关注的对象，毕业后的王康被分到一所中学教体育。理想、才华、诗书满腹的王康不满于做一个体育老师，他的目光盯上了当时中国启蒙的先驱者们，他在假期自费到北京做了人民日报记者刘宾雁先生的助手，他在当时中国一流的先驱、圣贤们身上寄予了美好的希望。虽然一开始他就远离了“第二种忠诚”，无论他的行藏如何，他只是忠诚于人性、历史和中国文化。 <br />
这个年轻人在运动不断的大陆中国当然只有颠沛流离的命运。他离开了体制，在大陆流亡，颠沛于是，造次于是。但他没有忘记他的梦想，“君子无终食之间违仁。”为了梦想，他做过生意，成立公司，像他的同龄人一样去学习做局、讲故事，但他的局和故事只是他的梦。他实在生就了思想家的头脑，却没有半点商人的思想。除了朋友接济，他的公司员工的薪水就全靠他的一支笔。慈不掌兵，他掌了兵却舍不得开除员工，员工们在公司里有心无力，无所事事。王康不开除员工，就只有拼命给人写稿子挣钱来给员工发薪水。这样的情况以至于朋友都看不下去，最后的结果，他的陪都文化研究中心和陪都文化有限公司还在，只是他一人和三四员工撑着而已。王康在坎坷的生计中养育了生命的元气，一种充塞天地历史之间的浩然之气。 <br />
<br />
三。蔡元培说：“近代学者人格之美，莫如陈独秀。” <br />
<br />
了解王康的散文作家魏真说，王康是“最浪漫最华美的中国人”。 <br />
在相当大的程度上，王康跟陈独秀属于同一个家族，这个神圣家族有着至大至刚之气，其个体生命的存在一直处于不止息的燃烧之中。这种燃烧既源于天命，近于神性，又来自于人格的自我欺许，基于人性又超乎常人的自信。 <br />
这种极富于神性和人性的自由放荡人格在历史上一再被祛魅，又一再为历史和后来所纪念。它最终被纳入中国文化，成为我们文化中极具悲剧性的美。因为它一再被一种世俗力量、乐感文化、生活理性、庸常宿命等等撕破毁灭。这种自由人格很少能够得到宽松的生存环境，它一再被打压毁灭的命运因为使得人性之美成为东土子民最罕见最神秘的要素。由精神、气魄等为元素的自由人格跟欲望、理性为元素的依附人格因此成为中国文化对立而不能统一的两极，摇荡着中国数千年的历史。这种神秘或现实之谜为高尔泰洞明，他说：美是自由的象征。 <br />
但是，由上层精英主导的中国文化没能厘清有教无类的生生德性，反而在非我族类的猜想里强化了独裁专横，无论政统还是道统都让道于正统，夷夏变异、汉贼不两立的正统意识，体制生存或正统中心主义因此成为超乎生命存在之上的绝对命令。二元多元之间的对立依存很少得到伦理共识和制度化的保证，反之，其此消彼长之势是中国文化认可的宿命。更等而下之的，莫过于文化心智中丧失了平等意识，对他者不是承认其自性，而是征用、管制、毁灭，并给自己美化、雄起化、神圣化。孙中山在革命生涯中得出痛苦的结论说：“……顾吾国之大患，莫大于武人之争雄，南与北如一丘之貉。”后来的钱钟书和费孝通在漫长的治学生涯里获得了文化自觉，钱钟书承认：“南学北学，道术未裂；东海西海，心理攸同。”费孝通则说：“各美其美，美人之美，美美与共，天下大同。”只是这种文化自觉只有少数人领略，历史进程中的事实更多地表现为文化的不自觉。 <br />
中国的现代转型，是一个祛魅的矛盾因而艰难的过程。它需要英雄、圣贤、先知们的自由人格，它需要美育引领走向世俗，一元专制的力量又会借世俗来打压神性和人性的自由，来扼杀人性之美；专制生活的“三帮分子”（帮忙帮闲帮凶）则会不断地跟风，不断地论证维护现实的合理性，不断地论证今天的进步和明天的美好。德性和道学、学问突显思想淡出、学人治人与专家治国、技术知识分子人文知识分子的朝野布局、人文精神与世俗理性、主义与问题、宏大叙事与微观关怀，等等，都曾为三帮分子们追随，他们在不同时期与时俱进地追随并论证一元的正当性，最终毁掉的是二元乃至多元的存在以及其共处依存的效用，他们最终打压了我民族自由的精神。 <br />
由此导致的精神危机在社会上最突出的表现就是，精英患上“犬儒病”，民众患上“冷漠症”。全社会对自由独立人格的避而远之、视而不见和冷漠，使得民族最优秀的个体总是得不到回应、声援，得不到展开的可能。时过境迁，犬儒者们又会百倍热情地给予前贤以怀念。现实对龚自珍、陈独秀、高尔泰们的态度即是如此，尽管历史已经还给了龚自珍、陈独秀、高尔泰们以无上的荣誉。 <br />
王康即是乡愿犬儒的年代里又一个自由的人格。 <br />
<br />
四。历史给予王康成全大命的机遇幸又不幸。 <br />
<br />
不幸的是，历史没有给予他上述历史人物那样的因缘际会，没有提供给他施展的舞台。历史只是给予了他和他的同学、朋友、同龄人们自我教养的可能。幸运的是，社会给予他的生存的缝隙，使得他能够自我成全，并影响周围。社会同样冷漠地对待王康，这种冷漠跟经济决定论的号召，跟闷声发财、一夜暴富、没事偷着乐的“幸福生活”形成了强烈的参照，反证出社会之恶。英国人萧伯纳认定：“对同胞最大之恶不是仇恨，而是冷漠；冷漠是无人性的本质。”诺贝尔和平奖得主、犹太作家威塞尔说：“对我而言，冷漠是恶的集中体现。”他还说：“爱的反面不是恨，是冷漠；美的反面不是丑，是冷漠；信仰的反面不是异端，是冷漠；生命的反面不是死亡，是冷漠。”亚辛斯基的箴言是：“不要恐惧你的敌人，他们顶多杀死你；不要恐惧你的朋友，他们顶多出卖你；但要知道有一群漠不关心的人们，只有在他们不作声的默许下，这个世界才会有杀戮和背叛。” <br />
王康身边有过很多理想主义者，他们来了又去了，去了的人悬置理想而务实，很多人做了高官、发了大财，只有王康还在坚持他自己的梦想。然后富贵起来的人们多作壁上观，看王康一人折腾，有人看王康行到水穷处就给他一点儿帮助，看他独自前行。 <br />
年过半百，知天命而望耳顺。但在王康却更为随性。随着电视、网络等传媒手段的介入，王康为更多的人认识，借助于口耳相传，王康成了精英权贵、仁人志士、热血青年们游历重庆的一个旅游景点。有人也专门坐飞机去重庆去看王康。于是，王康在重庆的生活，也多了一种倾听。他的时间多半在陪客人倾诉了，他是最好的听众，那些野狐禅式的年轻人，滔滔不绝地跟他说上五六个钟头，他可以一言不发。但他丝毫不给人安慰，他不吝惜最真实的言辞。当一个读书极富的年轻记者飞到重庆，到王康的小公司采访他，第一句话就是“我对政治没有兴趣”，王康的回应是“那你现在就给我滚！”当来访者在王康的办公室里高谈阔论半天，轮到王康表示，他开口的第一句话是：“我不喜欢你。”人心不同，各如其面；或者，相由心生。长期专注于自己的内心世界，王康甚至获得了相面的能力，“我一看你的长相就不喜欢你！”“不要把知道读书当宝！” <br />
王康不喜欢金庸，他自己却像金庸小说里的绝顶高手风清扬，如同风清扬超越了剑宗气宗的分别，王康的自由人格近于人文却超越了人文。他在立功立言立德的颠峰状态打量了所有来向他倾诉的人，他从来没有因知不足而矮化自己的心灵。 <br />
这个极为高贵的人却有着同样极为谦卑的心。他放浪形骸，却对女性给予最完美的体贴。一个打字员的喜怒哀乐会让他萦怀；一个一面之缘的女孩向他倾诉，他事后会再写上一封长信来安慰人家。画家王建称道老康，“有着不可思议的体贴心。”他不喜欢精英权贵，却跟重庆的普通民众交上了朋友，他当仁不让地教育他们，又极为谦卑、慈悲地布施、担当、同事。刘慰荣，跟王康的朋友做了一年车夫的出租司机，与老康接触了几次，就义无反顾地抛弃了从前的所有，而做了老康的司机，开始一种新的生活。席庆生，一个退休的生活得小康的工人，孩子在上海立业，他一人呆在重庆：因为重庆有老康。 <br />
王康的生存姿态甚至感动了媒体，凤凰电视台的众多编辑记者们关心他，不少人成为他的粉丝。2005年，《南方人物周刊》的编辑们在策划“最有味道的四川人”时，仍然把重庆的王康列入候选人名单，王康是入选中唯一的布衣。 <br />
王康因此给了他的朋友们最大的人生安慰。王康小传在网上流传，有人在前面上一句：“吾友，深邃、悲悯、纯洁，大隐于市的思想者与文学痴汉，读其文字，浩浩乎盈心者，人性人道之意浓焉。”这个小传中的王康是――“生于中国现代历史一个最关键的年头：1949年。这个年头使他成为一个宿命论者和天生的理想主义者。大学期间以独具的风骨和才华成为西南最高师范学府自1957年来第一个学生文学社社长，并因此自决于中国式经济仕途、学院翰林之外。耿于沉思，疏于著述，不求闻达，不意被封‘民间思想家’。二十世纪八十年代末以布衣之身撰写‘中国改革宪章’，名动京畿；九十年代初再以《大道》为题，撰写叩问‘中国往何处去’之五集政论片，论者称为‘冷战结束后对中国道路运思甚深的先知式作品’。同期有长篇诗评《俄罗斯启示》传布四方。抗战胜利50周年以九集电视片《抗战陪都》倾服众多业内人士；60周年又组织巨型长卷史诗国画《浩气长流》，尚未问世，已臻不朽。对中美关系、台湾悬案、中日现状以及马克思主义、港台新儒家皆有独到心得，自谓‘人世’未尽解，而‘天命’已略知。” <br />
<br />
五。“都称缔造者，孤魂自飘零。为人续家谱，痛煞后来人。” <br />
<br />
这是王康在江津陈独秀旧居留言簿上的即兴之作。他对一切真正的个人、真正的民族之子从来不吝惜热情。 <br />
王康熟知唐德刚、黄仁宇们的中国现代史观：在一个二百年的三峡论中，个人的存在几乎是微不足道的，都是历史文本借以书写的手段；即使按照传统王朝的史观，如果我们生活在大明季世，生活在同治光绪之际，生活在民国，我们能把一个民族、一种文化带出三峡或埃及吗？如果不能，我们生存的正当有效性有何依据呢？ <br />
王康身体力行的是唐、黄们忽视的现代转型中的个体价值。他从陈独秀先生的生平中看到了一种远高于历史目的、未来愿景更大的信仰情怀，一种植根于民族文化的人性之美，一种活在当下的大道和人生正义。西人说，实现公正，即使天塌下来。用王康的朋友毛喻原先生的话：做一个人，是中国国民最伟大的成就。 <br />
2006年底，余英时先生获美国“克鲁格人文奖”，奠定“西方学界继胡适之后不作第二人想的中国知识精英大师”地位。余英时是钱穆、唐君毅等人合力创办的新亚书院的第一届毕业生。在王康和余英时之间有着不小的距离，两人是不同气象的峰峦、大海。余英时是道问学的，王康是道成肉身、身为度、尊德性的。但才思超迈的王康仍给了严谨的余英时最高的敬意。 <br />
王康说，对中国历史的基本态度构成了中国社会的基本分野。一个是1949年以来，愈演愈烈的历史虚无主义，另一个是要接通历史，存亡继绝，这对中国来说太重要。中国没有西方式的宗教、历史就是中国的宗教。割断历史，我们已经尝到够深的苦果了。如果继续把历史虚无主义延续下去，一代两代三代四代五代，它危害的将不仅仅是中国。现在中国，一方面是官方史学界集体的道德沦丧，他们既愧对中国孔子、左丘明、司马迁以来伟大的古代史学传统，更愧对中国现当代历史。面对如此苦难、复杂、丰富的现当代中国，他们交出的几乎是白卷，更有一些人为虎作伥，粉饰太平，沦为专制和独裁的奴婢。</div>

							
							
							
							
