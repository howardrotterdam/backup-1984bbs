<br />
科学家的选择与责任<br />
&nbsp; &nbsp; <br />
<br />
――钱学森之死有感 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; <br />
<br />
王 康 2009年11月3日 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;钱学森死了。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;自然想到他的两位同行，美籍德国科学家阿尔伯特・爱因斯坦和俄苏科学家安德烈・萨哈罗夫。 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;爱因斯坦之所以受到全世界的景仰，除了他是相对论的创立者和现代物理学一代宗师外，就是他对人类文明事业与世界和平的终身关切。这位德国犹太人不仅表现了其先辈如摩西和本・迈蒙尼第等人将《圣经》与希腊哲学、艺术西方文明两大源泉融汇的传统，而且感人至深地表达了对二十世纪特有劫难的深度忧虑和异常深刻而富有启示性的思考。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;1932年，爱因斯坦致信意大利墨索里尼法西斯政府教育部长罗各，呼吁阻止威胁意大利知识界向法西斯制度表示忠诚的“残酷折磨”，告诫“墨索里尼先生不要对意大利知识界的优秀人物进行这种侮辱”。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;我不知道钱学森先生1955年（正是毛泽东对“胡风反革命集团”横加侮辱并将成百上千无辜中国文人投入监狱的岁月）回到中国来，是否与闻对俞平伯、胡适等人的精神围剿。短短的三个月内，钱先生就被委任为中国科学院动力研究所所长。在那个年代，如此重要、神秘的机构不经过极为严格的审察和旷日持久的考验，像钱学森这样由美帝国主义培养成才的物理学家，怎么能如此神速地晋升到新中国核工业最显赫的位置上，有历史证评癖的学人恐怕会发生兴趣。须知，那时朝鲜战争才结束两年，毛岸英死于美国轰炸机下，其父正发起反美、仇美、蔑美运动。钱学森那时似乎还不是中共党员，也无其他特殊身份，竟享受如此优渥厚遇，――恐怕不能仅仅以其专业或聪明解释。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;昨日上网，方知钱回国有二说。一则出于爱国，二则被美国当局递解驱逐。1949年钱正申请加入美国，怎么又突然对“解放战争”和“新中国”心向往之。一名由中华民国政府派遣赴美的国民党高官快婿、又在资本主义美国接受教育然后执教美国第一流大学的物理学教授，其“红色觉悟”来自何方？1999年，美国政府公布窃取美国国家机密者的“考克斯报告”，钱的大名仍赫然在目。其时中美建立已经二十年整，江泽民正声称欲与美建立战略合作关系。美国人没有必要“刺激”已到鲐背之寿且备受中国最高层膜拜有加的钱学森。那实在不利于美国自己的“国家利益”，要末就是那些死硬反华反共的FBI分子脑筋太死。当代“乾嘉学派”有责任弄个明白。至于我，对于前美军上校钱学森何故回到毛泽东这里来，了无兴趣。道理也很简单，毕竟是中国人，非要等到一个红色政权建立，才算是“为祖国效命”吗？<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;那之前两年，陈寅恪也受到郭沫若邀请，担任某历史研究所所长职务。前者以“精神独立和思想自由”为条件，以不宗奉马列主义为前提，慨然辞谢。五十年后的今人可以相信，陈寅恪先生的学识、人品及其对于中国历史文化的成就和挚爱，丝毫不逊于包括郭沫若、钱学森等党国知识权贵。事实上，陈先生代表的中国文化立场和方向，为郭、钱望尘莫及。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;爱因斯坦在那封信中认为，“无可怀疑的是，追求真理和科学知识，应当被任何政府视为神圣不可侵犯，尊重那些诚挚地追求真理和科学知识的人的自由应该作为整个社会的最高利益”。他强调，只有在这一基础上，西方才产生出伟大的文明。意大利文艺复兴才赢得世界的称赞。这是用纯洁的伟大的殉道者的鲜血换来的传统，才使意大利受到世人的爱慕和尊敬。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;我不知道，高寿近百的钱学森众多的仰慕者们会用什么辞藻为他送葬，至少，不会出现“自由”、“真理”、“殉道者”。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;1939年7月，爱因斯坦接受匈牙利物理学家西拉昆建议，给富兰克林・罗斯福写信，告知这位美国总统，费米、约里奥・居里等国际物理学家已经使从大量铀中建立起原子 链式反应成为可能，由此将产生巨大能量和放射物。如果把这种实验运用于军事，就会制造出“极有威力的新型炸弹”。他提醒说，德国外交部副部长的儿子冯・魏茨泽克领导的柏林威廉皇帝研究所正在进行着同样的实验。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;这封信导致了原子弹的诞生和广岛、长崎的核爆炸，并使第二次世界大战得以提前结束。世人因此称爱因斯坦为“原子弹之父”。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;但爱因斯坦1945年秋天在美国广播公司发表谈话，否认自己是原子弹之父。认为链式反应是德国物理学家哈恩发现的，是丽塞・迈特内从德国逃出带给丹麦科学家尼耳斯・玻尔而为国际物理学界普遍接受的。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;同年12月10日在纽约诺贝尔纪念宴会上，爱因斯坦致词指出，诺贝尔发明了烈性炸药，为了对此赎罪，为了良心上的宽慰，他设立资金以促进和平。“今天，参加过研制这种历史上最可怕最危险的武器的物理学家，不说是犯罪，也是被同样的责任感所苦恼”。他认为，“我们（主要指美国科学家）之所以曾经帮助创造这种武器，是为了预防人类的敌人（主要指德国）比我们先得到它；要是按照纳粹的精神状态，让他们占先，就意味着难以相像的破坏，以及对全世界其他各国人民的奴役。”他强调，“我们所以把这种武器交到美国和英国人民手里，因为我们把他们看作是全人类的信托者，是和平自由的战士。”我不知道钱学森先生被称为“中国导弹之父”（最近更名为“中国航天之父”），作何感想。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;我不知道钱学森何以对上世纪五十年代由美返华的历史如此耿耿于怀。他那些本事完全来自加州理学院冯・卡门、帕森斯等人。美国司法部门指控他为共产党员这一案情，也应成为考据癖者的调查对象。其实是不是共产党员本来无关宏旨，事实上钱1958年不是“入党”了吗？在冷战初期麦卡锡时代，钱因共党嫌疑被取消他参予美国最高军事机密工作，本是历史洪流中几乎不值一提的小事，他由此所生的怨恨却让我难以理喻。就算美国冤枉了这位共产党天才，不也就是几年赋闲在家，在一个风景优美的小岛上独居了14天而已，他那800公斤图书资料，就仅仅是一些简单的公开出售的工具书吗？<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;一名有共产党嫌疑的中国人，轻而易举地进入美国火箭研制喷气推进动力实验室，参予了研制原子弹的“曼哈顿计划”（当然只是一名小角色），还以美军上校身份参加“美国空军科学咨询团”，战后赴德国考察，这难道不表明美国制度的开放包容与慷慨大度吗？钱身涉政治疑案。加州理工学院同事募捐一万多美元为其假释，院长李・杜弗瑞德专程飞赴华盛顿为钱斡旋，并委托著名律师库珀为其作公开辩护，这难道不表明美国社会的人道关怀和公正精神吗？与毛泽东时代遭灾受苦罹难的数以百万计中国知识界相比，钱学森在美国经受的“不公正”乃是何等惬意的待遇，何等浪漫的阅历。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;据悉，中美建交后，加州理工学院将钱评为该院“杰出校友”，还赔偿其损失费，只是不作出道歉。钱因此拒绝回母校领奖。中国愤青于此又照例用了“气愤地拒绝”一类毛式修辞。天真的美国人很难明白，钱的凛然气节怎么就不对比麦卡锡时代恐怖一万倍的毛泽东们表现一下呢。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;我不知道钱学森对爱因斯坦、奥本海墨等物理学家持何种态度。香港凤凰卫视在钱死后以最快速度在“我的中国心”特别节目中，加于钱的头衔是“世界著名科学家”。不知该片撰文者是谁，稍稍了解现代物理学史的都知道，除了中国外，世界上没有几个人知道钱的大名。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;1957年毛泽东召见时，他禀报了有关“工程控制论”后，立即表示自己的理论“十分浅薄，一定有很多错误”，请毛指正。很难设想，爱因斯坦会以这样的自辱之辞向罗斯福表示谦逊。钱对完全不懂任何自然科学的毛泽东作如此卑贱的表态，即使在三十年代的德国和意大利也不会发生。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;1946年1月12日，爱因斯坦致信美国“全国科学家会议”，公开鼓动说，如果政府因为对俄国的恐怖情绪而试图加强对社会经济和政治生活的控制，那末美国科学家“就应当拒绝听从美国政府的不义要求，即使它有合法机构做靠山”。他提请注意说，美国科学家“拥有一种不成文的法律，那是我们自己良心上的法律，它比任何可以在华盛顿制定出来的法案要更加具有束缚力。当然，我们还有两件最后的武器：不合作和罢工。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;毛泽东曾在不同场合、包括1957年全世界共产党莫斯科大会上，声称中国不仅不怕、而且欢迎第二次世界大战，六亿中国人死掉一半也在所不惜。钱学森不可能不知道毛的类似主张，我不知道他赞成还是反对。但从其工作和行为看，他绝无反对可能性。这样，我就只能得出一个结论，世上并无一个具有共同文明价值和道德底线的国际物理学界，爱因斯坦号召与国家和政府不合作的号召，绝没有受到所有国家科学家的赞许。相反，至少在中国，从秦始皇到毛泽东，在他们焚书坑儒的历史火光背后，那些实用学科类知识，是得到国家政权的悉心保护的。量子力学创立人之一的海森堡曾在《物理学与哲学》一书中，试图证明自然科学作为人类智慧最强大的力量，终将使得哲学、尤其是政治法律思想与物理学的客观、真实、永恒和普遍意义达成一致。在1949年以后，西方一切理论，包括物理学都只能沦为权力的工具，钱学森及其工作，证明此论不虚。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;此外，中国举国之力发展导弹、原子弹、氢弹同时，四千余万以农民为主体的中国人死于饥馑。钱学森对此惨剧不可能不清楚。我不知道，在献身于导弹核武器丰功伟业时，钱学森如何平衡他的爱国心。制造几枚要么永远不使用要么一旦使用就意味着包括全体中国人在内的人类共同毁灭的核导弹与几千万同胞活活饿死之间，孰重孰轻？<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;爱因斯坦曾提醒罗斯福总统“在政府同在美国做链式反应工作的物理学家之间有一种经常的接触是可取的”。那是因为必须抢在希特勒德国之前掌握原子武器，无论在道义和逻辑上，这一理由都是正确的，起码是无可奈何的正确。同时，爱因斯坦和其他美国物理学家深知这种建议和抉择，隐藏着巨大的道德隐患和现实命运上的危机。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;1947年6月，美国哲学家卡尔・波普尔在布鲁塞尔艺术科学院发表演讲承认，纳粹主义和法西斯主义的失败“并不意味着野蛮和残酷已经失败。相反，那些可憎的观念也从失败中取得了某种胜利”。他痛心地指出，希特勒成功地降低了西方世界的道德水准，自由民主国家被迫使用了因受纳粹主义威胁而被迫研制的武器，“我们的文明可能最终毁于希特勒主义希望用于我们头上的那些武器”。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;不知道钱学森先生是否多少与闻或感知到二战后世界一流知识分子这些既深刻又寻常的观点。他是否真正认为，以饿死几千万中国人为代价发展导弹核武器，是他的爱国心的验证，这一验证是对中国民族利益的真正捍卫。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;此外，他理应比一般中国人更清楚，上世纪七十年代末，苏联勃烈日涅夫－格列奇科集团准备对中国实行“核外科手术打击”。因为中国拼命发展的核武库将会构成对苏联的威胁。只是因为尼克松－基辛格政府出人意料的反对，这场核浩劫才没有变为现实。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;1950年1月31日，美国总统杜鲁门宣布美国将制造氢弹。罗斯福夫人埃诺・罗斯福特别组织系列电视辩论，爱因斯坦应邀于1950年2月13日发表现电视讲话指出，如果杜鲁门总统的努力果然成功，那么大气层放射性毒化，以及随之而来地球上一切生命的灭绝，都将是在技术上可能达到的范围之内。他再次敦促美、苏两国消除相互的恐惧和不信任，不仅废弃大规模破坏性武器，还要郑重宣告放弃武力政治，同时建立起“超超国家的裁判和执行机构，哪怕只有一些国家宣告他们愿为实行这种‘局部性的世界政府’而真诚合作，也会大大减轻紧迫的战争危险”。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;这种声音在钱学森所服务的新中国，没有出现的可能。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;中国民族一百多年来历经内忧外患，从下到上有一种独立于世界民族之林的要求，拥有巨大的历史合理性。但是，与这种民族要求同样重要的是，中国个体生命大规模倾覆（尤其1949年以后）是这个国家同样不幸而亟待终结的悲惨状况。作为研制攸关亿万中国人生命的毁灭性武器的主要负责人，钱氏可曾真正严肃地评价自己的工作，并为此向最高当局陈述现状，分析后果，而不是无条件效忠于毛泽东第三次世界大战、世界革命的恐怖理论。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;“意大利科学协进会”于1950年10月在卢加举行第42次会议，爱因斯坦在贺信中郑重阐述了“现代科学家的道义责任”。他痛心地指出，他的许多同行“已经退化到这种地步：居然把国家政权强加给他的奴役当作不可避免的命运接受下来，他甚至自甘堕落到这种地步，竟然驯服地献出自己的才能，去帮助完善那些注定要造成人类普遍毁灭的工具”。这是20世纪科学界真正悲惨的命运，科学家“制造出了那些不仅会奴役他，而且还会从内心上毁灭他的工具，由于那些挥舞政治权力的人的压制，他不得不噤若寒蝉”。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;毛泽东丧心病狂而又荒诞愚蠢的暴行之一，是大办人民公社。这一举措导致了他的政权合法性和权力来源的基础――中国农民――的空前灭绝。毛的科学依据之一是作为物理学家 的钱学森关于粮食亩产超过十万斤的宏论。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;海德格尔因为出任柏林大学校长、为纳粹声辩而永远难洗其道德污秽。从2009年10月31日起，在另一个世界，是否会有数以千万计的饿殍亡灵会盯着这位“新中国最伟大的科学家”的眼睛问：三百六十行，何必多管闲事？<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;爱因斯坦在同一封贺信中，对曾产生了伽里略、布鲁诺、米开朗基罗、达・芬奇等科学巨匠和艺术天才的意大利同行表示敬意。他问道：那个曾经使科学家有机会对他的同胞进行启蒙而且丰富他们精神世界的时代，难道真的过去了吗？他的回答是：“一个天生自由和严谨的人固然可以被消灭，但是这样的人决不可能被奴役，或者被当作一个盲目的工具听任使唤”。中国漫长的专制传统和严重变形而惨遭暴君利用的民族精神，导致许多人不仅自甘工具，而且“创造性”地为专制独裁服务。爱因斯坦的回答对他们不啻是神话。――能与暴君同席，受其恩诲，是多少天生奴性和卑贱者的莫大幸事。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;1952年9月15日，日本《改造》杂志主编向爱因斯坦提出如下问题：1、您看到那些显示原子弹毁灭性后果的照片时，作何感想？2、对于作为一种人类毁灭工具的原子弹，你如何评价？3、大家都预料，下次世界大战将是一场原子战争，这难道不是意味着人类的毁灭吗？4、尽管您完全明白原子弹可怕的破坏力，可是您为什么还要参与原子弹的制造？<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;爱因斯坦坦率承认了日本人指控性的质询，同时也为自己做了必要的辩护。与13年前（1939年）他给罗斯福总统信中所陈述的理由一样，“德国人也会抱着完全成功的希望在同一问题进行工作，我那时只能这样做，再也没有其他选择余地，尽管我始终是一个虔诚的和平主我者”。最后他再次推崇“我们时代最伟大的政治天才”甘地，他为印度的解放所做的工作证明：由百折不挠的信念所支持的人的意志，比那些似乎是无敌的物质力量有更强大的威力。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;如果钱学森读过自己的女性同胞（也是他岳父最敬重的伟大中国女性）宋美龄女士1945年8月14日庆祝抗战胜利时在抗战陪都重庆发表的谈话，他也许会反思自己的人生角色，产生某种不安的心绪：“原子炸弹的制造方法，虽经严守秘密，然而这是根据众多科学家所明白的道理，若有相当的资源与时间，或许可以仿造的。如果落在贪婪无义的敌人手里，将造成大部份人类的毁灭。”毛泽东生前虐害了无数中国人，这与他不顾一切发展核武器的疯狂计划之间，并非没有联系。为这种无法无天之徒制造能毁灭人类的武器，与爱因斯坦和甘地的和平与非暴力主义者之间，何止霄壤之别。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;1953年7月，一名瑞士老太太向爱因斯坦提出两个问题：1、近几年来全世界到处出现极恶劣的天气，是不是由于原子爆炸的缘故？2、由于企图要把原子弹造得更有威力，我们这一代人有没有保护我们这个行星的大气层免遭破坏的危险呢？爱因斯坦在简短的加复中相信，人类已经有力量或者不久就会有力量，用放射性物质来彻底毒化大气层，使陆地上一切植物和动物的生命同归于尽。但他“还是十分乐观地相信，人类通过建立世界政府，大概早晚会避免这种毁灭”。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;建立世界联邦及其政府，是但丁、格劳秀斯、圣彼埃尔修道院院长、卢梭、伏尔泰、康德五百年间欧洲思想家的高尚理念，也是2500年前孔子大同世界的梦想。爱因斯坦身为犹太人，对为祸欧洲既久且烈的民族国家心存戒惧，天生怀有某种世界主义和人类情怀。他多次呼吁建立超民族超国家的世界性政府，虽然直到去世为止，这一理想都遥遥无期。但除非人类在国家神圣和民族至上必然导致的国际无政府状态和相互忌恨与仇杀中走向毁灭，否则爱因斯坦“天下一家”的伟大向往一定会刻在未来人类共同体的纪念碑上。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;我不知道钱学森先生如何评价爱因斯坦的世界主义，他多半没有深入思考过这个问题。一个世界主义者在毛泽东时代是注定要遭受迫害的。即使在今天，占人类四分之一的中国也断难容忍将联合国一类国际组织实质性地扩大。不久前奥巴马总统首次提议，有关国家通过协议，彻底销毁地球所有核武器，中国军方人士在国家电视台上公开表示质疑和反对。在不久前结束的阅兵式上，中国向世界展示了各种新式武器，其中包括战略核导弹。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;1954年11月18日，爱因斯坦在回答《记者》杂志关于美国科学家处境问题时，说了一句轰动美国社会的话：如果我重新是个青年人，并且要决定怎样去谋生，那末，我决不想做什么科学家、学者或老师。为了希望求得在目前环境下还可得到的那一点独立性，我宁愿做一个管子工，或者做一个沿街叫卖的小贩。美国“管子业工会”通过决议，授予爱因斯坦荣誉会员的称号。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;钱学森拥有众多头衔，官拜全国政协副主席，跻身国家领导人，这种际遇当然不会厌恶自己的选择，赌气去作一个管子工。这是又一种区别。美国接纳爱因斯坦和众多德国和欧洲犹太人，爱因斯坦在美国受到了他在德国不可能享有的尊荣、礼遇和研究条件。他个人拥有的自由权利、独立性和言论空间，比德国希特勒、苏联斯大林和中国毛泽东三大极权主义所有知识分子这三类权利的总和还多。尽管如此，爱因斯坦很少赞扬美国的民主自由，更从不表彰美国政府。相反，他不变的风格是永远批判现存制度，抨击资本主义美国，尤其不吝词汇指斥美国政要。这种爱因斯坦式的不合作与批判意识，正是现代世界始终不能被各种型号的专制力量欺骗、恫吓和奴役的精神防火墙。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;而钱学森先生，从1955年44岁到2009年98岁高龄辞世半个多世纪间，中国知识分子天文数字般罹难殒灭的悲剧，似乎完全与同样身为知识分子的他毫无干系。像1957年“反右运动”和1966年“文化大革命”那样的社会大惨剧和历史浩劫，人们从来不知道钱学森命运如何，这位似乎享有特别豁免权的神秘人物，奇迹般地置身于两代中国知识界宿命般的苦难之外，这一事实，也当成为考据派的题材。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;也许钱先生自有其人生观，有其不轻易示人的人生履历。但我对所有在上世纪毛时代免于贬抑的人士，皆作存疑。当然，以钱的特殊地位、影响和身份，他事实上一开始就不属于几百万知识分子他更不是如一般社会人文类知识分子，他是毛式红色帝国统治集团中的特殊成员，整个统治制度和战争机器中的特殊角色。明乎此，便可断定，爱因斯坦的“失落感”决不会降到钱学森身上。当然，前者摩西般的先知地位和圣徒形象，也为后者望尘莫及。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;1955年，冯・劳厄从西德去信，讯问原子弹问题，世界各地都有人不断提出类式问题。爱因斯坦于该年3月19日回复好友，最后一次答复“关于建议制造原子弹的动机”问题：“鉴于希特勒可能首先拥有原子弹的危险，我签署了一封由西拉德起草的给美国总统的信。要是我知道这种担忧是没有根据的（指德国的原子弹计划），当初我就不会插手去打开这只潘朵拉盒子。因为我对各国政府的不信任，不仅限于对德国政府。”在逝世前不到一个月前，爱因斯坦确实流露出十分黯澹的心绪：“很遗憾，我没有参与反对对日本使用原子弹的警告。这一荣誉应当归于詹姆斯・弗朗克，要是他们听了他的话，那就好了！”弗朗克也是从德国流亡到美国的犹太物理学家。1945年6月1日，由杜鲁门总统指定的研究原子弹使用问题的“临时委员会”（由陆军部长史汀生任主席，成员包括国务卿贝纳斯和物理学家K.T.康普顿等）一致建议尽速并且不作事先警告就对日本使用原子弹。当时在“芝加哥大学冶金实验室”美国原子弹研究机构中工作的弗朗克和西拉德坚决反对这项建议。他们与五个同事联名对史汀生发出备忘录指出，如果美国对日本使用原子弹，就会“给建立原子能的国际管理造成几乎无法克服的障碍，必然会引起核军备竞赛，严重威胁人类安全”。该备忘录是以“芝加哥社会政治影响委员会”名义草拟的，有七名科学家答名，并在1946年5月1日出版的美国《原子科学家通报》公开发表。早在1945年3月，西拉德就起草了一个文件，预言原子军备竞赛和洲际导弹的危险。他请爱因斯坦介绍他去跟罗斯福总统见面呈述这个备忘录主旨。爱因斯坦于3月25日为他写了给罗斯福总统的介绍信。但当时罗斯福已经病危，不久去世。杜鲁门接任总统后，把爱因斯坦的信交与贝纳斯处理。5月28日，贝纳斯约西拉德谈话，西拉德反对使用原子弹的态度异常坚决，6月初他与弗朗克给史汀生发了一个备忘录后，7月间又写了一封得到六十多名科学家签名的“紧急请愿书”，于7月17日送达白宫。但杜鲁门出于战争和政治考虑，于8月6日、9日在日本广岛、长崎上空投掷两颗原子弹。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;从以上简略叙述中，人们不能得到这样的印象：美国科学家，对于如使用原子弹这样重大的战争决策，从来没有众口一辞的附议。与爱因斯坦一样，良心、理智和道义感，始终是西方科学家的核心价值。这样的故事，情节，内幕永远不可能在斯大林、毛泽东统治下的社会出现。如果中国也面临因为战争需要而使用原子弹的情势，如钱学森这样的科学家会对最高当局已经作出的战略决策会作出什么反应，只有天知道。象西拉德、弗朗克和爱因斯坦那种言行，恐怕不可能出现。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;当“毛泽东思想万岁”重新出现在天安门广场阅兵式上，人们有理由质问：这名“与人奋斗其乐无穷”的狂徒关于世界大战、核大战的思想也将“万岁”吗？<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;钱学森已经作古，他的崇拜者们要求为他们的又一尊偶像举行“国葬”。 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;罗素本希望在苏联拥有原子弹以前，美国通过原子武器的垄断迫使斯大林接受国际秩序，确保世界自由与和平。1949年8月29日，苏联爆炸第一颗原子弹，罗素意识到鉴于美苏双方都拥有热核武器，并且拥有完全对立冲突的意识形态，建立世界政府以维护全和平不再可能。1954年3月1日，美国在太平洋中部马绍尔群岛比基尼岛上成功爆炸第一枚氢弹。罗素立即得出结论，核时代的战争根本修改了克劳塞维茨在其《战争论》中所宣称的“战争是政治的继续”那种陈旧理论。原子弹、氢弹的出现，根本改变了前此一切战争哲学和历史理性，它只有一种结局：人类集体自杀，同归于尽。1954年12月23日，罗素以82岁高龄在英国广播公司以“人之祸”（Man’s Peril）为题发表演讲，指出人类面临核战争的浩劫危险，引起世界范围内（除苏联、东欧、中国、北韩等共产国家外）强烈震撼。德国物理学家玻恩和法国物理学家约里奥・居里建议由诺贝尔奖获得者签署一封致有关国家的公开信，并召开一次有关核浩劫威胁的科学家国际会议，向各国公众强调核战争的毁灭实质。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;1955年4月5日，罗素把一份宣言寄给爱因斯坦。4月11日爱因斯坦在这封被后世称为《罗素－爱因斯坦宣言》的文件上签名，七天后即去世了。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;罗素刚参加完“世界议员促进建立世界联盟会议”，正从罗马飞往巴黎途中。当飞机飞越阿尔卑斯山上空时，机长告诉乘客，他刚刚收到新闻，爱因斯坦在普林斯顿去世了。罗素感到“心都碎了。如果没有爱因斯坦的支持，世界和平进程将被延误几十年，甚至一个世纪！”但当他到达巴黎旅店时，爱因斯坦签名的《宣言》已从伦敦转到。爱因斯坦的签名被视为“来自象征人类智力顶点的人的临终信息，恳求人类的文明不要被其愚蠢行为所毁灭”。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;由此，爱因斯坦第一个签名的声明（本来叫做《关于热核武器的声明》，后改为《科学家反对战争》，最后通称《罗素－爱因斯坦宣言》。罗素希望这个文本摒弃任何政治偏向，各国科学家的都将被考虑。后来，加入签名的著名科学家有：玻恩，德国理论物理学家，量子理论主要创建人，1954年获诺贝尔物理学奖；布里奇曼，美国高压物理学奠基人，1946年获诺贝尔物理学奖；英费尔德，波兰物理学家，爱因斯坦老友兼同事，爱因斯坦・英费尔德・霍夫曼理论创始人之一；约里奥・居里，法国物理学家，核辐射理论创始人，1935年获诺贝尔化学奖；穆勒，美国遗传学家，辐射遗传学创始人，1946年获诺贝尔生理学/医学奖；鲍林，美国量子力学/化学合成理论创始人，1954年获诺贝尔化学奖；鲍威尔，英国核物理与宇宙射线理论家，1950年获诺贝尔物理学奖；汤川秀树，日本介子理论基本原理创始人，1949年获诺贝尔；罗特布拉特，英国物理学家，1949年获诺贝尔和平奖。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;有几位西方科学家拒绝签名，他们是另外三名诺贝尔物理奖获得者：玻尔、哈恩和西格班。理由是：一，在他们有生之年甚至更久，科学情报和成果的自由交换只能在西方国家科学界进行；二，任何有共产党人签名的东西不可避免地会推动共产党人的目标。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;这份不朽的文献的作者不是以这个或者那个国家，这个或者那个大陆，这种或者那种信仰的成员资格来讲话，而是以人类，以其能否继续生存已成问题的人类成员资格来讲话。他们告诫道：这个世界充满着冲突；而使一切较小冲突相形见拙的则是共产主义同反共产主义之间的冲突。……知道得最多的人，也就最忧心忡忡。“我们中间的大多数人在感情上并不是中立的，但作为人类，我们必须记住，如果东方和西方之间争端的解决，对于无论是共产主义者还是反共产主义者，无论是亚洲人还是欧洲人还是美洲人，无论是白种人还是黑种人，都能给以可能的满足，那末就决不可用战争去解决这些争端，我们希望东方和西方都了解这一点。……作为人，我们要向人类呼吁：‘记住你们的人性而忘掉其余。要是你们这样做，展示在面前的是通向新乐园的道路；要是你们不能这样做，那末摆在你们面前的就是普遍死亡的危险。’”<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;宣言发表后，其副本分别运达美、苏、中、英、法、加六国政府首脑（包括中国总理周恩来，我相信这个副本和其他更多中国人完全不知情的西方知识界通过各种方式送达的类似文件，都被密封在北京某个机构中）。英国首相艾登很快表示，同意《宣言》基本精神和原则，“放弃核武器必须成为全面裁军的一部分”。美国国会议员和政府高官“高兴地赞同阿尔伯特・爱因斯坦和其他八位著名科学家的请愿”。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;苏联与中国政府对这份《宣言》一直保持沉默，直到现在。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;不难推断，作为《宣言》签名科学家的同行和晚辈，钱学森不可能不知道这份文件。我无法揣测他知悉这篇不朽文献时的感受。1955年7月9日，罗素在伦敦卡斯顿厅宣读这份历史性宣言时，钱学森正被美国政府驱逐回国，8月10日到达香港，11月即与钱伟长等建“动力研究所”。当爱因斯坦以生命的最后精力为他所属的这个世界签名留下一份将流传久远的伟大宣言时，他绝对想不到在他曾寄予满腔同情和希望的东方，一名他在美国的小字辈同行，会全身心地致力于他和他那些星光四射的科学家一致反对的毁灭性事业。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;受《罗素－爱因斯坦宣言》影响，1957年4月12日，联邦德国十八位原子物理学家和11位诺贝尔物理、化学、生物、文学、和平奖获得者联名发表《哥根廷宣言》，反对他们的国家国防军装备战术核武器。他们发誓，“无论如何，签名者无人愿以任何方式参与核武器的制造、试验和使用”。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;三个月后，1957年5月18日，32名诺贝尔科学奖获得者在德国康斯坦茨博登湖联名发表《迈瑙宣言》，向世界呼吁：“我们相信：科学本是通向人类幸福生活之路。但是，我们怀着惊恐的心情看到，也正是科学在向人类提供自杀的手段。贸然发动总体战的国家将加速其本身的灭亡，并将给整个世界带来威胁。所有国家都应当自动放弃使用武力来作为政治生活中的极端手段。如果他们做不到这一点，他们就将停止生存。”<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;在《罗素－爱因斯坦宣言》精神鼓舞下，来自东西方的科学家（包括苏联科学家塔普契夫院士）在加拿大新斯科舍省沃什村举行了首次科学家国际会议。到2005年7月在日本广岛召开以“在广岛和长崎后的六十年”为主题的科学与世界事务会议，帕格沃什科学家们共举行了55届年会。科学家团队从最初的物理、化学、生物和法律领域扩大到病毒学、精神病、政治、军事、历史、战略、社会学、国际关系，议题包括核武器、核裁军、军备控制，世界安全、科学家责任以及生物化学武器控制、太空武器控制、教育与人口增长、环境与生态、地区冲突、战争根源、艾滋病、恐怖主义和文明冲突等领域。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;帕格沃什会议对处理古巴导弹危机、结束越南战争、解决印巴冲突、中东战争等重大冷战和地区危机发挥了最具理性权威和公正原则的特殊作用。并直接促成了《禁止核实验条约》（1963年）、《禁止核武器扩散条约》（1968年）、《限制战略武器第一阶段会谈》、《生物武器协定》（1972年），成为从根本上改变核战争和一切战争的主要理性贡献者。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;如果没有爱因斯坦和他反对战争、尤其对核军备竞赛的深切认识，这个世界的面貌将大不一样。《罗素－爱因斯坦》和《哥根廷宣言》、《迈瑙宣言》以及帕格沃什运动，对防止第三次世界大战和核浩劫启示录式的毁灭性结局，发挥了任何一个大国政府不可企及的作用。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;杀戳、集中营、迫害、流亡，物质世界奥秘，上帝的旨意，天才、良知、智慧、忧患、责任、使命……，无数因缘造就了爱因斯坦。他的科学成就，关于宇宙奥秘的沉思，人格，以及他那犹太先知特有的面容、目光、蓬乱的头发和胡须，都将作为人类德性与智慧的象征，万古长存。无数人分享了他的心血和脑髓，我是其中之一。我在二十年前开始的流亡生涯中，画了爱因斯坦72岁时的肖像，挂在我那混迹于水泥丛林中的写字室的墙上。每天上班，我都会与他会面，觉得无论阴霾蔽日还是夕阳无限，都如此神秘而亲切。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;1933年4月初，隐居在比利时奥德斯坦附近勒・柯克海滨的爱因斯坦给普鲁士科学院写了两封信，拒绝该院关于他“参与了美国和法国和恶意诽谤宣传活动”的指控。还拒绝该院关于他在国外“为德国人民讲句把好话”的要求，并声明辞去其科学院院士职位，放弃普鲁士公民权。爱因斯坦的正式答复如下： <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;要我去做像你们所建议的那种见证，就等于要我完全放弃我终生信守的关于正义和自由的立场。这样的见证不会像你们所估计的那样是为德国人民讲好话；恰恰相反，它只会有利于这样一些人，这些人正在图谋损害那些曾使德国人民在文明世界里赢得一席光荣位置的观念和原则。要是在目前情况下作出这样的见证，我就是在促进道德败坏和一切现存文化价值的毁灭，哪怕只是间接的。 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;在此前一个月，爱因斯坦发表了《不回德国的声明》： <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;只要我还能有所选择，我就只想生活在这样的国家里，这个国家中实行的是：自由，宽容，以及在法律面前公民一律平等。公民自由意味着人们有用言语和文字表示其政治信念的自由；宽容意味着尊重别人的无论哪种可能有的信念。这些条件目前在德国都不存在。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;我希望比较健康的气氛不久会在德国得到恢复。我也希望将来像康德和歌德那样的德国伟大人物，不仅时常会被人纪念，而且也会在公共生活里，在人民的心坎里，以及通过他们所矢忠的伟大原则的实际遵守，而永远受到尊敬。 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;爱因斯坦有资格有根据发表这种声明。六百万犹太人在集中营被灭绝的惨剧证实了他对那个曾是他的祖国的厌恶和绝望。希特勒的覆灭、勃兰特和科尔先后在华沙和特拉维夫的下跪以及柏林墙的垮塌证实了他的预言和希望。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;一位把世界作为祖国，把人类命运和全球文明与和平内化为生命体验的伟大科学家，是人类共同的骄傲。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;1927年曾为爱因斯坦画像的巴伐利亚画家萨尔，1938年逃出纳粹德国监狱来到普林斯顿。他问一位老人，你对爱因斯坦科学著作毫无所知，为什么如此仰慕他呢？老人的回答是：当我想到爱因斯坦教授的时候，我有这样一种感觉，我已经不再寂寞孤单了。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;这也是我的感受。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;但对我的同胞钱学森副主席，我没有这种感受。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;我的父母都是研究自然科学的知识分子。在我很小时，他们偶尔会提到郭沫若、钱学森、李四光，口气很平淡。在报上看到钱学森那篇关于粮食高产的文章时，我父亲轻声说了两个字：荒唐。我的印象是，他们都是某种大人物，高高在上，与毛泽东、周恩来有特殊关系而已。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;我的爷爷奶奶都在大饥荒的1960年饿死。后来我知道无数老人、妇孺死于那个年头。那正是钱学森大力施展其科学才干的时代。我从个人家庭出发，对他不存在任何敬意。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;1955年4月18日1时25分，爱因斯坦与世长辞，法国物理学家朗之万1931年的一段评论被刊登在世界主要报纸上： <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;在我们这一时代的物理学史中，爱因斯坦将位于最前例。他现在、将来也还是人类宇宙中有头等光辉的一颗巨星。很难说，他究竟是与牛顿一样伟大，还是比牛顿更伟大；不过，可以肯定地说，他的伟大是可以同牛顿相比拟的。按照我的见解，他也许比牛顿更伟大，因为他对于科学的贡献，更加深刻地进入了人类思想基本概念的结构中。 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;爱因斯坦比牛顿更伟大，在对人类处境和命运的关怀方面，更是如此。这样的人物，他的葬仪极其简单，是十分自然的事情。没有宗教仪式，更没有任何官方参与，除几名亲朋外，下葬时间和地点一概保密。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;有人呼吁为钱学森举行国葬。已经设了灵堂，接受各方吊唁。对钱学森，这种后事安排也是十分自然的。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;其实没有可比性。除了都曾接受过现代科学教育之外，爱因斯坦与钱学森属于完全不同类型的人。在我写作这篇文字不断引用前者的资料时，心里一直有一种亵渎先哲的愧疚。这也是我们时代的一种无奈，占人类四分之一的生命共同体中，已经找不到可以让我们从那些僭据了至尊地位实际并无什么文明价值的事像人物中超脱出来，为真正永恒不朽的精神、道德、智慧的楷模而感念长久。 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;与钱学森有相对比较性的，是苏联物理学家安德烈・萨哈罗夫。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;1989年6月29日，“六・四”民主运动惨遭镇压25天后，钱学森被授以国际技术与技术交流大会“世界级科学与工程名人”称号，表彰他对火箭导弹技术、航天技术的重大贡献。全世界全中国都知道，这段时间是血腥、恐怖和黑暗的历史溃退的非常时期。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;不到半年，1989年12月14日，萨哈罗夫在莫斯科去世。苏共中央总书记米哈伊尔・戈尔巴乔夫与十几万莫斯科人一起，冒着严寒前往吊唁。我那时在深夜通过勉强清晰的美国之音听到戈氏对逝者的悼词：他是俄国历史上反复出现的巨大悲剧中离我们最近的圣徒式人物，他的精神将鼓舞我们这些在俄国又一个困难时期艰难摸索的人们而不朽。我被深深震撼。在同一个星球上，同一个历史大翻转关头，俄罗斯总有天才和先驱站在自由与奴役、光明与黑暗临界处，我为俄国庆幸。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;萨哈罗夫没有在西方接受自由、民主、人权精神，他是从极权主义的罪恶与苏联社会的危机中，作出了自己的人生抉择。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;他曾是苏联最年轻、前程无限的科学院院士。1948年，27岁的萨哈罗夫便参予了由克格勃首脑贝利亚主持的研制氢弹的“阿扎玛斯16号”绝密项目。几件现实事件震撼并改变了他的命运。氢弹试验基地是由政治苦役犯建造的，他们后来被残忍地处决了；1957年底在乌拉尔核废料处理场发生恶性爆炸，数千人死于非命；1958年11月苏共中央决定恢复核实验，萨哈罗夫第一个发出抗议；1960年10月，为苏美首脑峰会，苏共中央决定向月球发射火箭，造成现场涅杰林元帅和许多火箭技术专家全部丧生。萨哈罗夫在自传中写道，“我从此变成了另外一个人。我决定和周围决裂”，他从此把同情（关爱）、自由、真实（compassion,freedom,truth）作为自己追求的三个目标。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;1964年，萨哈罗夫联合了24名著名知识分子和艺术家致信苏共中央总书记勃烈日涅夫，“警告”他停止为暴君斯大林恢复名誉。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;1968年，萨哈罗夫“进步的反射，共存和知识分子的自由”一文在《纽约时报》发表，被称为继恰达耶夫1830年发表《哲学书简》近一个半世纪以来俄国漫长历史黑夜中又一声警钟，“苏联极权制度的判决书”。它第一次系统地阐述了建立民主、多元、人道社会的思想。该文很快传遍全球，复印数达1800万份。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;正隐身在梁赞市附近一座寒冷农舍里创作长篇小说《1914年8月》的索尔仁尼琴看到这篇文章后感叹道：萨哈罗夫是唯利是图、绳营狗苟的苏联社会中的“一个奇迹”。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;这一年，钱学森正式受命负责筹建人造卫星、运载火箭以及卫星探测器。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;我不知道苏联发生的变化可曾为钱先生知悉。被称为“苏联氢弹之父”的萨哈罗夫至少是钱的苏联同行。促使萨哈罗夫改变人生轨迹的那些不幸而荒谬的悲剧是否也在钱学森的视野中发生。如果也曾发生，那么他的态度难道没有丝毫改变吗？<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;在苏联这样一个绝对主义社会和极权主义国家里，大俄罗斯主义与共产主义结合成一种远远超过历代沙皇不敢奢望的自豪和狂热。1979年苏联出兵阿富汗，各阶层人民和知识界出现空前狂热的爱国主义浪潮。萨哈罗夫通过国际媒体，率先谴责这一侵略行径。在爱国者掀起的汪洋大海上，他的抗议几乎被吞没得无影无踪。人们称他为“叛徒”、“犹大”、“藏在苏联社会阴暗角落里的老鼠”。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;普希金、恰达耶夫、赫尔岑、陀思妥耶夫斯基、托尔斯泰、列宾、肖斯塔科维奇、茨维塔耶娃、阿赫玛托娃、索尔仁尼琴都是伟大的爱国主义者，但他们是清醒、诚挚、真正的爱国主义者。大俄罗斯主义始终不能阻挡他们挺身反抗俄国丑陋、野蛮、冥顽不化的专制统治和蒙昧状态。谴责奴役、欺骗和残忍，坚持对自由、慈悲、宽容和精神解放的渴望，两百年来一直是俄罗斯高尚爱国主义的灵魂。萨哈罗夫是二十世纪新斯大林主义统治时期真正爱国主义的英勇代表之一，戈尔巴乔夫的评价绝非过誉。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;我从网上下载了钱学森生平简介。在萨哈罗夫与苏联极权主义决裂以后十数年间，钱学森成为中国火箭导弹和热核武器最显赫的领军人物。1970年5月1日，钱在天安门城楼受到毛泽东接见。那时傅雷、老舍、吴晗和大量高级知识分子已经含冤而殁，中国20世纪最圣洁的英豪林昭已被残害……。我不知道钱被毛那双沾满中国人鲜血的软手握住时，有何种感受。他当然不会知道，就在那座城楼上，毛泽东与林彪的仇恨已经公开化。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;在这段时间，萨哈罗夫公开对苏联内政外交发表了独立而深刻的评论。钱学森没有对中国更加黑暗危殆的社会问题发表只言片语。中国人民和知识界的苦难似乎完全在这位聪明世故的“科学天才”的视野之外。用职业特殊性为其开脱是不能成立的，萨哈罗夫的存在如山一般严峻，在大洋彼岸，人们不会忘记爱因斯坦、奥本海墨等大科学家。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;萨哈罗夫由此付出的代价，是命运对他的合理回馈。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;他被苏联科学院开除，褫夺公职。1980年，他与妻子被押解到高尔基城，监视居住，与世隔绝。长达六年。其间，萨哈罗夫两次绝食抗议，密秘警察强行灌食。在十分艰险的条件下，他不得不把自己长达1000页的自传复写三遍，前两次都不翼而飞了。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;在高尔基城，经常可以看到这样的情景：世界最著名的核物理学家、“苏联氢弹之父”与夫人形影相吊互相搀扶而行，手上提着沉甸甸的布袋，里面装着一经发表就会让世界感慨不已的手稿，后面不远处，永远跟随着几名克格勃人员。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;这是20世纪最独特的人文景观。一名本来可以以其忠顺的服务让世界颤栗的全球最大核武库掌门人，却因小小的良心而在自己的国土上流亡，在两千多个日子里只有爱妻相守。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;命运并非如此寡情。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;1975年6月，萨哈罗夫完成《论国家与世界》一书。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;34年前发表的著作，似乎是为二十一世纪的中国写的。我相信如果在中国国内公开发行，对于我国人民，包括党国领导层，也包括钱学森这样几十年间似乎没有机会更新自己历史常识的人物，都是一件有益的事。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;鉴于萨哈罗夫的卓识产生于与中国高度同质的社会中，因此对于我们中国知识界，它们在某些方面甚至比爱因斯坦的睿智更富于启示性。不可能在一篇即兴文字中引用太多――它已经够长了，但是萨哈罗夫的见解实在太深刻，它们似乎是对我们的直接陈述，我必须征引其中若干段落，――这样的段落其实比比皆是： <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;成千上万生气勃勃和看来是安于天命的人在矗立于莫斯科空际的、漂亮的新阿尔巴特摩天大楼脚下来来往往；但是，在这一堵面墙后边，隐藏着旁观者难以了解的许多事实，隐藏着众多人间的不幸、艰难、凶暴、残忍、极度疲惫和麻木不仁，凡此种种积累了数十年，损害着社会的基础。在国内有许许多多不幸的、命运不济的人：领取微薄退休金的退休老人；生活坎坷者，他们没有工作，或者没有机会学习，或者没有适当的（甚至是按照我们的低标准而言）住房；不能得到治疗的慢性病患者；无数酗酒、腐化堕落者；150万永远被抛出正常生活的囚徒，他们是司法机器的牺牲品，这些司法机器是盲目的、经常不公平的、出击灵魂的、投靠当局和地方帮派势力的；一些纯粹因不善于及时行贿而倒霉的人。对于所有这些人实际上无法给与帮助，而且也很少有人打算这样做，因为，总的环境是：大多数居民为糊口而劳碌奔波，少数人脑满肠肥，安于故步自封，社会结构则是装装门面而效果很差。绝望的人们包括高级机关的接待室，其中特别令人嫌忌者直接被送进了精神病院。我十分喜爱祖国的大自然和文化、以及祖国的人们，我绝对不想当“抹黑者”的角色。可是，现在我认为必须集中注意那些被苏联宣传和亲苏宣传所掩盖的不良特征，它对于国际关系以及国外对我国情况的了解具有原则性意义。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;当前的苏联社会，在我看来，应该最简洁地称之为“国家资本主义社会”，也就是这样一种社会：它和西方类型的现代资本主义之间的差别是，在经济以及文化、思想和其它主要生活领域内实行全盘国家化，党和政府的全部垄断。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;全盘的国家垄断，正如许多作者指出的，必将转为不自由和被迫遵从，因为每个人都要完全依从国家。在危机时期，不自由造成恐怖，而在比较安定时期，不自由则造成无能官僚的统治、愚昧无知和麻木不仁。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;全世界应该了解克里米亚鞑靼人的无边苦难，31年前，他们是罪恶驱逐出境措施的牺牲者，当时有半数的孩子和老人死于冻饿，而现在依旧无权返回那等待着他们热爱劳动的双手去劳动的克里米亚故土。伏尔加河流域的日尔曼人、迈斯赫地区的土耳其人的命运同样如此。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;外国客人们有时提问说：如果你们这里确实有这许多缺点，老百姓不设法纠正吗？这个问题三言两语道不清。制度稳定性的因素之一在于物质生活水平毕竟在提高，虽然提得缓慢。每个人当然不会拿自己的生活同远不可及的巴黎作比较，而是和自己贫困的过去相比。但更重要的是，极权主义制度内在的稳固性在于恐惧感和消极感的惯性作用。任何国家的人民都没有在一代时间蒙受过如此无比的苦难。我们的工人不是英国的工人，甚至也不是那些贫困了可以上街的波兰码头工人。虽然广播扩音器每天在提醒普通苏联公民，说他是国家的主人。但他十分了解，真正的主人是那些：他们早晨和晚间坐在黑色保险轿车里，沿着寂静的、交通遮断的街道飞驰而过。他没有忘记，他的祖父的土地是怎样被没收的。他知道，今天他个人的命运也完全依靠国家，依靠身旁和远处的领导，依靠房屋管理委员会主席，依靠工会基层委员会主席（这位主席可以安排、也可以不安排他的孩子进幼儿园），也可能还依靠在他身旁工作的克格勃告密者。在选举时，他投入票箱的选票上只有一致的姓名。他不能不体会到这种“没有选择的选举”在政治上给他以何等侮辱，不能不感觉到这种庄严的仪式里包含着对健全的理智和人类的嘲弄。人们训练他，他为了生活，也接受训练。他自我欺骗。苏联公民是极权主义社会的产物，暂时也是这个社会的支柱。我只能祈求命运，希望在摆脱这种历史性绝境时不要产生我们现在还难以想象的巨大激荡。所以我是一个进化论者，一个改良主义者。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;党和国家在文化和意识形态领域内实行垄断，其后果特别有害。思想划一化经常地――从学校课桌到教授讲坛――要求人们虚伪、顺应、愚昧和装傻。不断玩弄那种令人哭笑不得的、宗教仪式般的、普遍宣誓效忠的把戏，把事业、健全心智和人类尊严等一切意念都排挤到次要地位。作家、艺术家、演员、教师以及人文学者在如此可怕的思想压制下生存，以至于人们不得不奇怪，艺术和人文科学为什么在我们国家里没有完全消失。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;思想一元化、排斥异己，加上冷酷的（虽然不是合乎理性的）政治现实，导致对持不同政见者不断进行迫害。看来，在苏联有2,000至10,000人可以称为政治犯。这个数字不包括因其宗教信仰而遭殃的人，他们的人数当然更多。也应该预先说明，我们的报导可以说是很不完全的。一切政治犯按现行法典都算是刑事犯，在我们这里不存在政治犯的单独地位，政治犯和其他类犯人（经常也是无辜的犯人）同受生活的苦难和侮辱，其卑劣性质是我们时代所不容的。试图泄露犯人囚禁生活的细节是会遭到残酷迫害的，这也就最好地证明了真相内幕。但毕竟有许多事是为人所知的：繁重的强迫劳动，经常是破坏了安全规章的；吃不饱肚的恶劣伙食，实际上又不能靠邮递和代交食物加以改善，邮递和代交食物是严加限制的（我们看到，甚至在判决前的羁押期间也存在这类限制），严格限制接见、通信和读书；任意施加残酷处罚。政治犯为自身人权而作的斗争（近来人们听到许多英勇的罢工和绝食事件）照例只是造成新的迫害。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;索尔仁尼琴、夏拉莫夫、金兹伯格、德耶科夫、奥列茨卡和其他数百名见证者和研究者描述过那灭绝两千多万人的古拉格群岛骇人听闻的制度，苏联监狱制度就带有古拉格群岛制度的许多特征。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;苏联政治犯是些什么人呢？他们中间的大多数人都没有犯任何罪行（就民主国家对罪行两字的解释而言），没有从事暴力行动，也没有号召从事暴力行动。政治迫害的通常原因之一是阅读、保存和向友人传递一些被认为内容不妥的（虽然经常是实际全然无害的）自发性手抄刊物和书籍。在这类成为被捕和定罪原因的书籍目录（不存在任何禁书“目录”，但每个人应该自己捉摸）里有：帕斯捷尔纳克的《日瓦戈医生》、阿赫玛托娃的《安魂曲》、贝尔加耶夫的《俄罗斯共产主义的起源和意义》、奥威尔的《一九八四年》、格罗斯曼的《往事俱逝》、本文作者写的《谈进步……》、阿夫特尔诺夫的《权术》、索尔仁尼琴的《古拉格群岛》、德热拉斯的一些书、康克维斯特的《巨大的恐怖》以及《时事纪事》、《市民议会》、《立陶宛天主教会新闻》和其它许许多多自发性刊物。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;在因信仰而受难的人们中间，有许多是信教者。宗教压迫是一切社会主义国家的可怕传统，但是，也许除在阿尔巴尼亚以外，很少有其它地方像在苏联一样，达到这样的广度和深度。早在20至30年代，一些最普通的宗教――东正教和伊斯兰教――曾经蒙受打击，遭到重大牺牲。现在，这些宗教的地位是如此低下和无权，以至于（至少在表面上）几乎成了国家的附属品。我这样说，绝不是要贬低教徒的信仰和内心抗争的作用。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;特别应该谈谈一些人的遭遇，他们被判刑是由于关心蒙冤者的命运，争取公开审讯和正义。受这类遭遇的人有苏联保卫人权创议会会员列奥尼得・勃留西，他在德聂伯罗彼特罗夫斯克精神病院遭受可怕的折磨，近似精神谋杀；有布科夫斯基和格鲁兹曼，他们因揭露精神病疗法的迫害情况而分别被判处七年徒刑；有不久前被捕的安德列・德维尔道赫列波夫和谢尔盖・柯瓦廖夫以及其他许多人。事实上，正是这些人进行了那种可以称之为“民主运动”的工作，压迫和崇高的信念使他们团结在一起，他们决心听从良知和信念的召唤。尽管这些人为数甚少，基本上集中在国内的两三个大城市，并且没有组织上的联系，但是他们在板结的苏联社会中存在，这一事实本身的道义价值是十分巨大的。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;我确信，保护苏联政治犯和共他持不同政见者，为争取监狱中人道待遇以及争取人权而进行斗争，这些不仅是全世界正直人士的道义责任，而且是直接保卫了正直人士自己国家里的人权。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;我和本文的大部分读者一样，从来不认识这些人，但他们的遭遇不能不令人震惊，让我们想起了中世纪囚徒的、以及我们时代里的那个不幸的赫斯的遭遇。我提到赫斯，虽然我知道他曾参加与建立罪恶的纳粹制度。但是，终身监禁几乎等同于死刑，我是根本反对死刑的，不管其所犯罪行的严重程度如何。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;几个世纪以来，许多国家的思想家――他们中间有贝卡里亚、雨果、托尔斯泰――曾经坚决呼吁废除死刑这种不道德的、不人道的和有害的制度。近年来，死刑在大多数发达国家里已被废除，但在苏联，这一步骤被认为是“不适时的”，于是每年都根据多种罪名――从情节严重的凶杀到“大规模盗窃国家资财”、遭禁的外币交换以及其他为西方法律所不熟悉的理由――枪决7000至1,000（按我的粗略估计）。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;拯救国家，在于国家和全世界的相互影响，而不拯救全人类，是不可能拯救国家的。必须进行触及一切生活面的民主改革；国家的未来在于转向进步、科学、恢复个人和社会的道德。不能仅仅依靠宗教思想、民族主义思想或者卢梭精神的某种传统意念来限制这种恢复的途径。谁也不该设想迅速彻底解决一些问题。我们大家要学会耐心和容忍，但要把耐心、容忍和思想的大胆和坚持不渝结合起来，可不该号召我们的人、我们的青年去牺牲；在我们国家里，人们完全依赖于国家，国家不呛气地吞噬每个人，至于谈到牺牲，为数已经是够多的了。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;制度的内在特点在对外政策表现上造成了重大后果。我将在以后各章中谈到此点。现在我只是着重指出若干情况：首先是社会的闭塞和极权主义的、脱离现实的领导作风，领导可以秘密作出决定，事先不经任何公开讨论，他们可以毫无控制地动用资金在其他国家进行秘密活动。还有十分重要的是，我国和外部世界的一切关系，包括秘密和公开的外交、商业、科学、宣传方面的关系，都可以由唯一的计划、唯一的意志任意加以控制。所有这些特点使苏联对外政策带有特别的素质――十分多变、实用主义的无原则性，它们表现在，比如，大量供应苏制武器支持乌干达的阿明、利比亚的卡扎菲以及其它许多国家的残暴制度，支持尼日利亚的种族残杀，支持伊拉克的库尔特族人以及利用世界许多地区的民族、宗教和政治仇恨以扩大影响。这些特别的素质使苏联政府在必要时就轻易地破坏任何协议，能在其他国家搞秘密破坏活动，包括收买、欺骗、讹诈、组织“第五纵队”。当然，这些特点给全人类、给原本已经十分复杂的书面增添了危险，加剧了挑衅。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;我经常不自由自主地感到不自在，几乎是一种羞惭的滋味。我做对了没有？我想到那些不可胜数的人们，他们从事显然直接有效的工作――种麦子和甜菜，建房屋、桥梁和制造汽车，治疗儿童和补牙齿，写诗和在实验室工作――他们自忖对人们是有用的，并且向往着个人的幸福。不，我没有背叛他们中间的任何人，没有给他们的劳动和幻想抹黑，也没有背叛我自己以及自己的希望（这种自我背叛可也是一件很不好的事情）。也许我从事这项工作的才具不足，对于完成命运托付给我的这项任务来说，智慧不足，缺乏概括力、观察力和生活知识。但如果我的内心是诚实的，那么，我就没有必要非难自己，而我的工作，正像任何其他劳动者的工作一样，也定然会是有效的。 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;也许我已经偏题了。钱学森的去世本是这篇文章的主题。但是第一，他老先生的资料实在太有限，我只知道几代党国要人都曾接见和看望过他。而按我的标准，对无论是中国儒家士大夫或西方自由主义知识分子，这种恩宠和待遇都与他们的道德文章无关；第二，萨哈罗夫的一生拥有古希腊悲剧英雄人物和基督教圣徒的双重灵光，尽管他既不崇拜希腊英雄也不是宗教徒。最后，借题发挥本是一种文体，它们正是我郁积心中早就想吐露的心绪。在此意义上，钱学森之死为我提供了这样一个契机，我应对此表示感谢。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;1975年10月，挪威奥斯陆诺贝尔委员会将该年和平奖授于安德烈・德米特里耶维奇・萨哈罗夫。该委员会主席奥瑟・丽安丝夫人在演说中强调，诺贝尔委员会是一个独立机构，独立于任何政府当局、政党、团体和个人，该委员会不受任何形式的威胁或方便妥协或其他机会主义的支配。她称，授予“我们时代伟大的人权斗士之一”萨哈罗夫是该奖的一大荣誉。“萨哈罗夫开展了强大的、不妥协地反对滥用权力和所有形式的违反人的尊严的斗争。萨哈罗夫令人信服地证明，人的不受侵犯的权利是真正的、持久的国际合作的唯一可靠基础。”她介绍说，萨哈罗夫32岁就入选苏联科学院，两次被授予列宁勋章，一次斯大林奖，三次成为社会主义劳动英雄。1968年萨哈罗夫由对苏联当局的失望而独立地选择自己的命运。该年他发表了自己的“宣言”：《进步、和平共处和知识分子与自由》，他意识到地球上的文明毁灭的威胁，那将是一场核战争的后果。他坚持认为这种危险只能以超越国家和意识形态的合作才能避免。他相信苏美两个超级大国之间的和平共处是可以实现的。为此苏联必需实行民主化、非军事化、反官僚主义和社会改革。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;1970年，萨哈罗夫和其他苏联知名人士建立了“人权委员会”，要求当局废除秘密审判，制定新闻法，保障人民获得资讯的自由，改革监狱制度，大赦政治犯，废除死刑，开放边界。1970年8月1日34个国家（包括苏联）在赫尔辛基签署《欧洲安全与合作条约》，重申“尊重人权和基本自由，包括思想、良心、宗教或信仰的不分种族、性别、语言或地域的所有人的自由”，宣布鼓励和促进“公民权利、政治权利、经济权利、社会权利、文化和其他权利及自由方面富有成效的实践，所有这些对于人类内在的尊严、对人类自由和充分发展至关重要。没有任何国家、任何政治家可以任何借口压制和逃避这些条款所规定的道德和政治义务。那样做的话，就是对人类与和平的背叛。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;这些条款精神显然包含和吸收了萨哈罗夫的思想。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;丽安丝夫人代表诺贝尔委员会宣布，“萨哈罗夫对和平的伟大贡献是，他以特别富有成效的方式，处在极为困难的条件下，以最伟大的个人牺牲精神，为取得对人类不可或缺的价值观念的尊重而奋斗。这些价值观念，正是赫尔辛基条约的基本原则”。萨哈罗夫为和平、为裁军、为所有国家之间的合作的奋斗，是将和平作为最终目的，由于他在唤醒每一个国家中广大民众上所做的努力与贡献，我们今天在这里授予他1975年诺贝尔和平奖来表示敬意”。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;应该就诺贝尔和平奖说几句话。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;这项奖已经颁发了109年。两次世界大战、冷战和核大战的威胁与危机都没有中断它不屈不挠的历程。谁都知道，不是每个获奖者都名符其实和无可争议，但占相当比重的获奖者，确实是人类文明天空中闪耀的明星，是人类苦难而伟大历史的忠实记录者、推动者和开拓者，是灵性、觉悟、真理、自由、美丽、善良、希望、信仰和爱的托命者，也是世界苦难、不幸、罪恶、暴行、不义的承受者和反抗者。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;1949年产生的这个中国，迄今为止与这个奖项无缘。这与我们5000年伟大文明和古老传统，与占人类四分之一人口的最大生命群体，与我们巨大的苦难与牺牲，与近三十多年间依靠数以亿计的农民苦力的血汗劳动、5000万海外华人真实不虚的爱国义举，西方近现代科学技术的大规模引入、尤其苏共极权制度解体、冷战结束（萨哈罗夫有特殊的重大贡献）、真正的全球化时代的来临而积累的巨大物质财富，与不久前上演的“建国60周年”的宏大叙事是如此让人匪夷所思的不相称，不匹配，不平衡。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;公允而论，以美国为首的西方自由国家，对苏联和中国奉行的是二十世纪最大的双重标准。在全球范围内阻遏苏联列宁－斯大林式共产主义的蔓延扩张，是威尔逊、邱吉尔等西方政治家最庄严最重要的世界战略，而戴高乐、尼克松等欧美政客始终执行善待中国的政策。几乎所有西方思想家都厌恶、否定俄国历史传统和宗教文化，却对中华文明向来不吝赞许之辞。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;据说钱学森去世前曾向温家宝先生提出一个问题：为何中国的大学研究机构培养不出杰出人才？这被称为“钱学森问题”。他心目中的杰出人才，应当包括诺贝尔奖获得者吧。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;萨哈罗夫是苏联自己培养的科学家，从共产党的阶级观看，基本纯正可靠。他却仅仅因为良知、正义感和对真理的信念而遭受厄难。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;钱学森是“旧中国”培养的“旧知识分子”，取了中国国民党一级上将之女为妻。顺便提一句，当年美国当局录用钱，绝不仅仅因为他年轻聪明（这世上聪明的年轻人太多了），而是因为钱是美国政府庚子赔款经费支助的留美学生。更重要的是，美中两国是并肩抗击法西斯的盟国关系。如果钱真有共党嫌疑，在美苏冷战初期最激烈争夺胜负的核武器领域，美国有关机构解除其工作，有什么值得大惊小怪呢？<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;事实上，1946年，中共安全情报负责人康生就开始在海外（主要是美国）招募华裔科学家，特别是火箭和核能专家。而康生曾长期在苏联受训，一度直接听命于贝利亚。谁都知道，毛泽东在1949年6月30日发表其《论人民民主专政》一文，不是公开宣布在苏美两大世界阵营中，社会主义只能有一个头，那就是苏联、斯大林，毛泽东不是宣布绝不骑墙，绝不走中间道路，中国共产党只能“一边倒”吗？如果钱是中共党员，在斯大林还有好几年才去世、中苏两党还需要十年时间才反目成仇时，岂不是在美国最重要的军事机构里直接为苏联培养人才吗？<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;因此，与萨哈罗夫相比，钱学森的国际背景、个人经历和服务宗旨显然更加复杂。至少可以说，钱学森的选择带有某种“历史的必然”。其实抗战刚刚结束，国民政府在百废待兴、内战硝烟升腾的窘境中，就把杨振宁、李政道、李远哲等青年学生送往美国普林斯顿高能物理研究中心深造。他们不是也成为世界知名的物理学家了吗？以几千年经世致用的儒家传统，在现代经验科学和应用技术方面，中国人确实享有某种优势。我的结论是，没有钱学森，也有其他人会填补上他的位置，那是毛泽东一代中共领袖的国家和世界战略的题中应有之义，耗费一切代价都在所不惜。何况，1955年11月二钱（钱学森、钱伟长，后者在1987年由许良英、刘宾雁、方励之发起召开的反右运动30周年纪念会议一事中，充当了告密者的角色）领衔组建科学院动力研究所时，中苏尚在“蜜月期”。沃尔比约夫、古谢夫、尼古拉耶夫、加夫里洛夫、萨韦利耶夫等苏联专家还在大量培训中国人员。二钱的高升，与毛泽东在莫斯科会议上大谈第三次世界大战招致赫鲁晓夫反感从而开始现出中苏裂隙有关。在毛看来，苏共20大以后中苏两国将出现意识形态分歧，因而必须另起炉灶。没有这一背景，在美国呆了二十年的钱学森们不可能被迅速受到青睐，即使他们早已是中共党员或马克思主义者也不行。 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;1971年3月5日，斯大林去世18周年良日，萨哈罗夫致信苏共中央委员会总书记勃列日涅夫。就政治迫害、公开性、信息交换自由、信仰自由、民族问题、边境开放、国际关系和若干内务外交政策等阐述了看法。最后指出：我国面临的问题与20世纪整个世界危机密切相关。这些危机是国际安全危机，社会发展失去稳定、意识形态陷于绝境、对理想感到失望、民族主义和非人道化的危险。萨哈罗夫特别强调，基于苏联在世界上的特殊地位，建设性地、谨慎地、灵活而又果断地解决我们的问题对全人类将具有重大的意义。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;三个月后，在没有得到回复后（在斯大林－毛泽东式国家中，像这样真正富于责任感、建设性和启发意义的信函，几乎没有回复的可能。这种国家的统治者似乎已经忘记了礼尚往来之道。抑或庞大的官僚机构使这类信函根本不能送达。不管怎样，新统治者不提供辩论可能，也拒绝进行任何实质性的对话），萨哈罗夫在国外把它作为公开信发表，并追加了一篇“后记”。再次表明自己的心迹：“我在认识到可能发生的热核战争和大气层试验热核武器的罪恶性质之后，约于10－12年前开始从事社会活动。”此后他在苏联发起了一场履行赫尔辛基协议有关系款的人权运动。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;关于“运动”，萨哈罗夫特别辨析说，不应该引起关于任何组织或团体、甚至于党派的想法。仅仅是通过某种共同行动方式结合在一起的社会现象。至于他自己，“任何时候也未扮演思想家或者领导者的角色；每次公开发表的言论，所反映的仅仅是对某些使我激动的问题的个人意见。”<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;说得多好！没有人会怀疑萨哈罗夫的勇气。对于他而言，道德关怀永远是第一位的。为更有效地实现自己的道德目标，理性、逻辑、分析和判断的准确以及现实条件可能达致的限度，对萨哈罗夫这样科学家出身、在世界上最强大最神秘的核武器制造中心机构工作多年的物理学家，极为重要，是他的重大责任和职业荣誉。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;正是这种理性精神，使他不仅代表了苏联持不同政见人士的良知高度，而且表达了苏联社会自我变革的本质要求；使他不仅获得国际科学界的普遍支持与世界和平人士的共同关切，而且对赫鲁晓夫到戈尔巴乔夫等苏联最高领导人发生了罕见的积极影响。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;1969年4月，中共九大召开，林彪的政治报告号召准备打核大战。苏联军方鹰派人物制定对华“核外科手术打击”时，萨哈罗夫是最早最坚决的反对者。他与索尔仁尼琴一样，深切同情“处于牢固的，一点也不比我们轻的极权主义制度的控制之下”的中国人民。但是同十亿人口的国家作战，将是一场启示录式的梦魇。俄国在一战中损失了1500万人，二战中损失了3000万人，如果同中国打仗，将损失6000万人，“这样的战争后，俄国人在宇宙中将不再存亡”。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;这里我要引用索尔仁尼琴的有关观点。他在1973年9月5日《致苏联领导人的信》中专门谈到“与中国的战争”。在他看来，如果中苏之间开战，将是人类战争史上最荒诞最可怕的一次。交战双方都将举起共产主义意识形态的旗帜，为了保卫列宁著作中第533页上的神圣真理，而不是敌对一方所认为的第335页，将有6000万俄国人和十倍于此的中国人死去。他称，俄国头脑清楚的人士已经“毫无成见地看到”：这种共产主义思想体系的黑风，是上个世纪末从西方刮来的，它折磨和伤害了我们的心灵。如果它现在继续吹向东方，那么就让它吹吧！（这并不是说，他希望中国精神上毁灭）。他相信，我们的人民将治愈这种病，而中国人也会如此。――他希望不要太迟，要尽快地挽救我们的国家和保护人类。但是，经受过太多苦难的我们，目前只关心挽救我们的人民就已经足够了。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;作为核物理学家，萨哈罗夫看得更远。如果开战，苏联强大的核力量将轻而易举地摧毁中国还很有限的核武库。苏联核专家们对于它们太熟悉了，所有的基地、准备都曾是他们直接领导的工作对象。但是，后果不堪设想，第一，苏联会在全世界名誉扫地，成为历史上最臭名昭著的战争罪人；第二，中国可能向符拉迪沃斯托克和其他苏联远东城市投放核武器，那将造成东北亚和日本的极大恐慌；第三，核打击造成的放射性毒气将弥漫大气层，所有国家都会受到影响；第四，美国会容忍苏联使用核武器以解决政治冲突、哪怕是两个共产主义大国之间的意识形态冲突吗？<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;从1968年与苏共核工业决裂以来，萨哈罗夫念兹在兹的就是两大问题：每个人的权利、尊严、生命的不受侵害，防止人类坠入共同毁灭。他在与著名大提琴家罗斯特罗波维奇等人的谈话中预言，美国决不会坐视苏联与中国之间的核战争。他在精神和价值观上了解美国，“这个伟大的自由新大陆责无旁贷的使命就是维护世界和平，遏制任何可能的核冲突。”苏联强硬派有种论调，毕竟苏联与美国都属白种人为主的国度，而且有某种相似的宗教、文化背景。因此美国人在中苏战争中起码会保持中立，何况中共头子毛泽东是那样一个热衷于暴力和战争、不可理喻的恐怖型暴君。国防部长格列奇科元帅甚至认为，苏联解除中国的核武装，是美国想做而不能做不敢做的一件天大好事，对于包括美国在内的全世界人民，都具有极其深远的意义。苏联后贝加尔军区司令扎哈罗夫元帅撰文，称1945年发生在远东的两事事件共同导致了日本的投降，马林诺夫斯基元帅和华西列夫斯基元帅率领的苏联远东军对日本关东军的“铁筒般的围剿与粉碎”，以及美国对日本广岛、长崎的原子弹轰炸。1969年8月18日，苏联驻美国大使馆授命正式知会美国高层，征询对中国发动核进攻的反应。萨哈罗夫断然拒绝这些分析，他强调，“一旦开战，只有一种结局：万劫不复。”<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;1960年的古巴导弹危机和1969年的中苏核战争危机，是20世纪两次最严重的世界性危机。它们最终未能酿成核浩劫，归根结底是在两次世界大战后、尤其核阴云笼罩下的人类拒斥共同毁灭的意志占了上风。其中，萨哈罗夫这样与共产极权国家战争政策决裂的物理学家的良心力量，与爱因斯坦等西方物理学界的人道传统，成为最令世人信服和赞许的道义与理性因素。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;我希望这两大传统也为中国科学界所认同并作为他们工作的指南。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;苏联政府禁止萨哈罗夫前往奥斯陆领奖。丽安丝夫人对此“深感遗憾”，并将此与1936年希特勒政权阻止前集中营囚犯卡尔・奥西埃茨基领奖事相提前论。萨哈罗夫的妻子丽娜・波尼代表丈夫出席仪式，并代为致答辞。我把1975年12月11日，萨哈罗夫为纪念诺贝尔而发表的书面演讲附在本文后面，以表示对他的由衷景仰。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;萨哈罗夫的人生原则是，绝不做奸臣，也不做忠臣，一句话，“不做臣民”。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;他被誉为“人类的良心”，不论在俄国还是全世界，他的一生都是追求自由与和平的人们的希望与精神的灯塔（美国前总统乔治・布什）。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;他是二十世纪占人类近一半人口的共产极权国家中的圣徒和先知，是俄国十九世纪十二月党人以后伟大自由精神的继承人，也是中国知识界的道德矜式，是中国一切向往自由、尊严与人生及宇宙神圣的人们永远的精神先驱。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;1986年底，戈尔巴乔夫打电话给萨哈罗夫，邀请他与妻子迁回莫斯科。萨哈罗夫成为苏联第一届国会中首位发言者。他没有附和戈氏的“新思维”与“公开性”（它们已经够开明不凡了），而是公开要求结束共产党专制制度。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;与爱因斯坦一样，萨哈罗夫把他许多精力和时间用于非物理学专业的社会问题。这正是世界科学界的一个伟大传统。在探索宇宙奥秘、赞叹大自然壮丽同时，真正的科学家必然会在内心深处唤发谦卑、感恩和救赎这三种神圣感受，也就必然会抗议一切人类不幸、丑陋和苦难，就必然会拥有殉道者式的命运。按中国传统，这就是“求仁得仁”。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;钱学森也发表了一些与其专业不相干的言论，除1958年6月16日在《中国青年报》和《大众哲学》上发表《粮食亩产会有多少？》一类无稽之谈外，又对地理、教育乃至沙、草产业和气功发表了与其“科学泰斗”身份太不相符的议论。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;关于“气功”和“人体特异功能”，钱认为将产生一场21世纪新科学革命，甚至比20世纪量子学和相对论还要伟大。我本人曾接触过严新、张洪堡等“大师”，我的印象是，在他们那些故弄玄虚的表演中毫无神奇，更无伟大可言。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;钱学森在“反右运动”和“文化大革命”中的言行，我不得而知。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;除此之外，历史不会忘记，钱学森、何作庥等“科学家”曾在“清除精神污染”、“反对资产阶级自由化”的“斗争”中，坚决站在专制当局一边。历史不会原谅，在一代青年人为中国的自由、民主、文明付出青春、鲜血和生命代价的1989年，钱学森站在刽子手和屠夫一边的纪录。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;1937年，“德国国家社会主义工人党”上台执政，一名德国技术物理学家以扫出任纳粹物理学会主席。他向戈林、希姆莱等纳粹头目证明，物理学及其教学和基础研究，是战争事业至关重要的部分。他建议说，一切不利于德国最高战略利益的知识分子和理论，都应加以清除和驱逐。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;在我们时代，一直站在自由、人道主义和多元共存对立面的科学界人物，钱学森算是他们的代表。应该以某种特殊的人道关怀提请那些丧失独立立场的“科学工作者”，注意和检点自己的选择，慎重思考自己的责任。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;现在，这位“伟大的爱国科学家”去见他的祖宗德国犹太人卡尔・马克思了。钱学森认为，马克思主义哲学是人类对客观世界认识的最高概括，也是现代科学技术（包括社会科学）的最高概括。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;1929年，恩格斯的弟子和秘书爱德华・伯恩斯坦将其《自然辩证法》手稿送给爱因斯坦过目。一名斯大林主义者霍耳登在为该书英译本所作的导言中写道，无产阶级革命领袖们的经典理论具有神奇的功效，他本人曾经用阅读列宁格勒《哲学笔记》的方法治愈了多年胃病（这篇导言因此传遍欧美两大陆，当然引起了嘻笑：俄国人太幽默了！）。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;爱因斯坦浏览了恩格斯那本书后对人说，它没有任何科学价值。又一名斯大林主义者斯特鲁伊克暗示说，爱因斯坦刚刚看了几页《自然辩证法》，就被其重大价值所吸引。1940年6月17日，爱因斯坦就此回复说：爱德华・伯恩斯坦把全部手稿交给我使用。我的评语是指整个手稿而言的。我坚信恩格斯本人如果能够看到，在这样长的时间后，他所作的谦逊的努力竟被认为有如此巨大的重要性，他会觉得这是很可笑的。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;据悉，马克思和恩格斯对一切阿谀奉承都深恶痛绝。一些中国人如毛泽东之流，声称死后到马克思那里报到。这又使我想起了一则故事。列宁去敲天堂之门，看门的保罗问他找谁，答曰求见马克思，又问是他什么人，再答曰“马克思的利息”。又据悉，马克思在门那边听到了，当着保罗的面转身走了。毛泽东们如果早早知道了这个小故事，他们大概会萌发一点自知之明，不再提“报到”事宜。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;认祖归宗，是中国每一个农民和小民无需灌输的信条。伟光大的毛和聪明绝顶的钱活到耄耋之年怎么就非要认一名19世纪的德国人为祖宗呢？何况人家并不领情。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;赵越胜先生近日有《辅成先生》问世，可字读。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;周辅成先生也活了98岁，他是“仁者寿”的典型。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;我不知道钱学森属于那种类型。我相信，在他死后两天时间有一个不相干的人写了这么些拉杂文字，并且把他与爱因斯坦和萨哈罗夫并列，是过于抬举他了。 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; <br />
