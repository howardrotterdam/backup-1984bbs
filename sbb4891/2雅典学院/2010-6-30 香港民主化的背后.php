　　香港一向号称政治沙漠，经济动物。然而，在中国对香港恢复行使主权、顺利度过第<br />
一个十年之后，香港却风云突变，大有成为第二个“政治过热”的台湾之势。仅今年以来<br />
，就发生如下罕见的冲突事件：香港民主派不顾人大释法，不顾违背宪制性文件《基本法<br />
》，坚持2012年进行双普选。为此不惜发起大规模的示威游行，甚至于2010年元旦冲击中<br />
央政府在香港的机构中联办，造成一名警察受伤。更为离奇的是，五名来自不同选区的泛<br />
民主派议员(公民党、社民连)，集体辞职，逼迫立法局进行补选，并打出“五区公投”、<br />
“公投起义、全民起义”的刺激性口号。此外，根据香港中文大学的民意调查首次发现，<br />
受到政治纷争及官民矛盾日益加剧影响，香港已不是和谐社会，约150万港人(占香港人口<br />
的四分之一)认同采取激烈方法，迫使特区政府响应民间诉求。<br />
<br />
　　就在此一刻，香港立法会正在审议2012年政制改革方案，中央和特区政府更罕有地公<br />
开赞同由香港民主党提出的、主要涉及区议会功能界别立法会议员选举办法的改良方案。<br />
<br />
　　香港的政治风云表面上是香港的民主化(快、慢)之争，但如果从更大的历史视野看，<br />
却极为复杂。这需要先从英国殖民政府为何坚拒香港民主化谈起。<br />
<br />
　　1840年鸦片战争的胜利，带给英国一个香港殖民地。而港英政府在香港确立的以总督<br />
为核心的政治制度一百多年都几乎保持不变。其核心是：港督代表英国对香港行使主权，<br />
代表港英政府实行治权。但总督的直接上司是英国殖民地大臣，他既不能对外签署外交文<br />
件，虽然是三军总司令，但也无权直接指挥驻港英军;香港立法局制订的法律不得与英国<br />
的法律和殖民地大臣的训令相抵触，英国政府可以否决认为不当的香港法律，甚至有权替<br />
香港立法。司法权上，香港法院的终审权在伦敦。对于香港立法局和行政局，所有议员均<br />
由港督委任，港督还同时担任两局的主席、拥有紧急立法权和解除立法局的权力，从而确<br />
保港督拥有绝对的控制权。而这种整套宪政体制的目的就在于强化英国对殖民地的绝对控<br />
制。<br />
<br />
　　英国这种做法的初衷，则来源于北美独立的教训。当年正是由于北美殖民地拥有独立<br />
的议会，不仅严重阻碍了总督权力的行使，而且议会对总督的监督使其变成催化独立运动<br />
的政治中心。显然，为了避免再次出现北美独立这种严重损害英国国家利益的事件发生，<br />
才是英国坚决不在香港推行民主的根本原因。更何况，当年的北美殖民地都是由同文同种<br />
同源的英国人所建立的，尚且追求独立，更遑论异族的香港。这正如英国殖民大臣1894年<br />
给港督的信中明确指出：在华人占多数的情况下，英国不能放弃香港的殖民地位，也不宜<br />
采取选举。相对应的，就是对华人实行种族隔离政策。直到第八任港督轩尼诗时代才取消<br />
了对中国人的歧视政策。并任命了第一位中国人(即伍廷芳)出任立法局的非官守议员(但<br />
由于英国殖民大臣的反对，不得不改为临时议员)。<br />
<br />
　　二战结束后，世界进入冷战。由于大陆内战，大量资金和廉价劳动力来到香港、朝鲜<br />
战争使得香港成为内地贸易和走私的秘密渠道，再加上东南亚政治乱局导致南洋华商将资<br />
本移至香港，同时港英政府采取土地公有、政府拍买的政策，香港经济得到迅速发展。但<br />
也引发了贫富分化、上百万人无房可住的现象。民族矛盾和劳资矛盾迅速激化。在这个背<br />
景下，发生了规模浩大的“67抗英运动”。虽然这场运动被港英政府残酷镇压下去了，但<br />
也使港英政府意识到，如果进行普选，左派力量将主导香港政治。但另一方面，随着英国<br />
交还香港时间的临近，港英政府也需要培养忠于自己的中产精英阶层。用在印度积极推行<br />
英语教育最得力的MACAULAY勋爵的名言就是：这个阶级有印度人的血统、印度人的肤色，<br />
但有英国人的嗜好，英国人的看法、道德及思想。其目的在于，哪怕将来英国人走了，他<br />
们留下的制度也只有他们培养的人才能操作，从而保持形式上不是英国人实质上仍是英国<br />
人统治的效果。此外，今天香港左右派分裂、对峙的历史根源，也在这里。在这种情况下<br />
，港英政府一改过去统而不治仅仅从经济上榨取香港的策略，进行大量的社会改革。如房<br />
屋计划、反腐、重建公务员体系。进而在八十年代推出现在香港民主派强烈反对的“议会<br />
功能组别”，将大量的香港本土精英纳入。<br />
<br />
　　从以上回顾不难看出，英国政府之所以不在香港推行民主原因只有一个：国家利益。<br />
不管是避免北美独立事件再现，还是避免亲大陆左派进入和控制权力中枢。这也是为什么<br />
当英国签署《公民权利和政治权利国际公约》时，特别明确宣布这个公约关于普选的规定<br />
不适合香港。现在香港的法律界普遍认为普选的法理不是来自《基本法》而是来自《公民<br />
权利和政治权利国际公约》，真是历史的玩笑。<br />
<br />
　　然而，当末代总督彭定康走马上任之时，何以英国又突然积极的推动香港的民主化了<br />
呢?这里需要指出的是，职业政客(时任保守党主席)彭定康的任命，打破了只有外交部或<br />
者殖民部官员出任总督的惯例，也显示包括随后的政改风波都是英国有意识的战略。<br />
<br />
　　现在看，原因不过有四。一是英国已经在香港培养出一个效忠于英国意识形态的中产<br />
精英阶层。它已经不担心如果普选会出现左派控制权力的后果。二是英国即将把香港交还<br />
给中国，如何实现不是英国人的英国人统治，推动民主化是必要的手段。三是不管推动香<br />
港民主化成功与否，英国都是赢家。成功了，自然会选举出亲英的执政团队，如果由于中<br />
国的反对失败了，自然会造成香港和中央的对立以及其内部的对立和混乱。而且英国把香<br />
港民众对民主的期望值推的越高，可能造成的失望就越大，对中央政府的不满就会越强，<br />
香港社会就会越分裂。英国实是拿中国的利益香港为筹码与中国政府讨价还价，可谓无本<br />
生意的稳赢不赔(现在西方拿西藏、台湾说事，也是同一思路)。第四则与时代大环境有关<br />
。冷战的结束，中国成为西方唯一遏制、肢解的对象，而香港就成为双方较量的最前线和<br />
主战场。英国调整对华政策，实是追随美国对华政策的变化。这也同样可以理解，何以八<br />
十年代，英国可以对中国让步，原因就在于当时的中美战略同盟关系。<br />
<br />
