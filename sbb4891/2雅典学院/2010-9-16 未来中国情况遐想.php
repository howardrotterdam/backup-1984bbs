													<h2>未来中国情况遐想</h2>
						
						
																											<div id="postmessage_831703" class="t_msgfont">[转帖]<br />
按现在的时局来说，维稳的打法也就坚持到小胡下台之后，接班人面临的问题远比小胡接手时来的严重的多，甚至可以说谁接班，谁8成就是末代总书记<br />
<br />
从历史的轨迹上判断，共产党要想完成政治上的延续（有力延续，不属苟延残喘），必须就离不开整肃（肃反，清理，随便什么名字，知道意思就好了），党内部高层范围内的小整肃，扩大到党外民众中的大整肃，小的基本就是在高层换届之后，枪毕的枪毙，圈禁的圈禁，基本不可能给你留什么余地，大整肃，好比苏联30年代大清洗，中国60年代文革，之所以我个人不是很爱研究邓以后时期的领导层与体制道理比较简单，党内真正的一些有能力的实干者，有点战略眼光的精英分子，都被清洗掉了，身上被戳了窟窿，或者是脑袋搬了家，后面跟上添空补缺的这些2流货色，其实都是酒囊饭袋，研究他们，还不如多抽空看点球赛来的实际，这类人里（中苏都算上了）最典型的就是勃列日捏夫了，要不是有大清洗，这小子一辈子最多就是个军政委退休（以他本人的实际能力也就在这位置了），怎么可能给大家留那多经典笑话，这些并不是凭工作业绩，也不是凭多大的人脉，完全就是因为补缺补上位的2流货色，对外交往不明白，对内治理不明白。。。。。。。。。。。说远了，先说下届天子，当今太子的处境<br />
<br />
说实话，现在谁也说不清楚到底这个人是谁，但研究是谁已经没必要了，所有他面临的问题，全部客观摆在那，先从国际环境说起，军事方面的C形包围网其最后一环阿富汗方向也基本铺设完毕了， 我估计米国最佳的设想并不是常规部队能在这C包里面取得多大的突破（主要目的还是在于封锁）而是把反导系统架设在这C圈上才是真正目的，到时候中国这点可怜的核投送能力就要被限制在本土，会形成这么一种奇怪状况，米国忌惮中国东风，因其不确定到底能不能打的下来，中国也忌惮米国的初中段反导，忌惮能不能突的过去，2相抵消，中国的核威慑力将大幅减小（这也就是为什么俄罗斯虽然弹头也过万，却依然反对北约在东欧部署反导）常规武器方面的劣势我更不必说，我说的其实也都是中国历来面临的军事上的老问题，只不过由于米国的军事战略部署重新调整回东亚地区（结束了中东嘛）这个C包问题会在未来10年内逐步升温，其次，91年以前由于是有苏联在前面撑着，所以美国跟中国基本是和平外交（友好往来型）但91年后至今经过2任总统的试探之后，大体上已经完全摸清了中国的这种有延续性的对外策略（人家是一届一个样，我们是几届都那样）所以类似于今年这种黄海军演的情况未来只会更甚，不会减弱，未来10年乃至更长时间，C包对中国就是个紧箍咒，收放程度不由我主，这种时紧时松的情况几乎贯穿了整个冷战时期苏联，最终成为迫使苏联解体的一大外因，这个外因，从黄海军演就正式开始套在了大陆头上，最近的钓鱼岛争端，日本如此坚持的根本原因，也是对大陆这种几十年如一日的对外策略做出明确判断之后看准了才走的一步（日本人的严谨，可比欧洲德国人）<br />
<br />
其次经济方面，中国所谓的经济崛起是房地产泡沫（上海房价数年间翻了十几倍）与非法的投机（哎，它就是尊法，那法也不健全）倒把幕后交易的所累计起来的结果，这种经济形式，脆弱不堪，一但受到国际市场（某些大国为主导的）有预谋的冲击，那后果就是毁灭性的，直接把中国经济推进万劫不复。把中国推回到改革开放初期的阶段，如何做出巨变，改变现有的经济模式，这也是太子要面临的问题<br />
<br />
死穴，社会问题，2015年后，有3个年龄段的人，会处于分工不同但意识相近的情况，就是思变，首先来说70后，2015年后这批70后就会有相当数量的共产党中层干部，<br />
老脑筋的60虽然仍占据高层，但实际问题完全就是这些70后在做，60后之前的几代人在中国来说并无本质上的不同，当初的思维模式都是被统一过的，但70后完全不同于之前的几代人，可以说到时70后将会是新社会的中坚力量，80受西方文化冲击之后带来的思维方式上的转变更大可以说是思变人群中的忠实拥护者，90后，说实话，眼下来看这代人真可说是一无是处，中不中洋不洋的一群人，但很肯定一点的就是，GCD这几个字在他们眼里完全就是一种贬意，不入流的典型代名词，可以说90后那时将会是最为狂热的一群（7080多已成家，热不起来列。。）干柴已经堆好，就看那时是什么火星。。。。。<br />
<br />
总体来说，太子服正之后，面临的是这3大问题而尤以第3个最为严重，最为紧迫，其实不伦是谁上了台，继续维稳的套路肯定是玩不转了，那样被赶下台是早晚之事<br />
<br />
3条路，继续维稳（这个人胆子小，但好赌）<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;由下至上的大清洗，搞好了可在延续30年，搞不好瞬间玩完<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;做重大改革，解除掉党禁，其他党派（比如台湾，那时也就和平解决问题了。。。。。）参政，实现民主政体（这样的话至少作为一个党派，共产党还是得到了保留）<br />
<br />
此三步，走最后一步，不会有流血，而且真乃我中国之幸<br />
走第一步，明显是条死路，共产党肯定消失<br />
要是走第二步。。。。。。。。不好说了，万般情况都有可能发生，比如内战，比如分裂，比如进入另一个怪圈<br />
