<br />
维族人是小偷、骗子；那那些在批发市场批了假货再去地铁站穿着传统服装兜售银饰，宝石的藏族人就不是了么；那些你看不出是什么民族却同样在作恶的人就不是了么；还有我们这些汉族看我们做的事难道我们就不是小偷、骗子、强盗、暴徒、杀手了么。<br />
<br />
维族他们有的语言文字，宗教信仰，以及文化要比一般的少数民族完善的多（蒙古族和壮族已经被汉人同化的差不多了，藏族要离开西藏相对困难些）所以他们很容易抱团。有个可能是教义的原因让他们变得别的民族的人更团结。而一抱团有时候反而体现的是某些人的特性而不是一个团体的特性。在我看来中共，抑或是早已有之的愚民教育让人丧失了独立思考的能力，人们会去自觉的追随那个抢先占有话语权的人，并不假思索地认为某些观点、做法就是正确的，或是应该的。比起想”该怎么做“，照着做，和按别人说的做容易多了。他们中有些人成为小偷，成为激进的民族主义者，我认为就是这个原因。难道单单就是他们么，从跟随“王侯将相宁有种乎”的农民起义军，到文革时期的红卫兵，再到现在那些到墙外看过几眼就开始追随“民运人士”争取“自由”的人，又有多少区别呢。</div>

							
							
							
							
