20万中东商人，你自己信吗？ </blockquote></div>伊斯兰教东穿与黄巢“灭回”问题&nbsp;&nbsp;文/济南的清真寺 <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; [历史] 阅读：85次 发表：2009-11-16 16:54:28 <br />
<br />
 <br />
<br />
文：行云流水 <br />
<br />
唐末发生的黄巢大起义，从根本上动摇了帝国的统治，开启五代十国的分裂和战乱，不管什麽“立场观点方法”的史书都有记载；但黄巢入广州屠杀穆斯林事件却鲜为人知。千年之後的读者首先可能产生的疑问，就是“唐朝时中土有穆斯林吗？”要回答这个问题，需要对伊斯兰教的东传进行一番简单的回顾，然後对“灭回”事件的责任和性质进行分析。 <br />
<br />
（一） <br />
<br />
伊斯兰教由穆罕默德（570-632）於公元七世纪初在阿拉伯半岛活动。“伊斯兰”是阿拉伯文的音译，原意为“顺服”，指顺服安拉的旨意，又一说意为“和平”。也有译为“回教”的。一般中文书上只说伊斯兰的根本经典是《古兰经》，创教者是穆罕默德，实际上伊斯兰教也将犹太教《旧约》和基督教《新约》奉为经典，也尊基督为先知。当然，在新旧约之外将《古兰经》作为根本大法乃是伊斯兰教区别于其他二教的特色，穆罕默德的先知地位也一样。 <br />
<br />
伊斯兰教的传播很快，到八世纪时教徒就遍及亚、非、欧三大洲，可以说，和阿拉伯的兴起互为因果。穆罕默德从麦加转移到麦地那的时间就是希吉勒历法纪年的开始（622年7月16日），但是直到逝世，他的活动并没有越出阿拉伯半岛的范围。他的继承人阿布・巴克尔担任哈里发（632-634）以後，派出大军进攻东罗马（拜占庭）统治下的叙利亚、约旦、巴勒斯坦等地，他的继任欧麦尔时期（634-44）则完全征服了叙利亚，并扩展到伊拉克、美索不达米亚和埃及等地。 <br />
<br />
阿拉伯人在近东建立了根据地後，下一步征服的目标就是波斯（今伊朗）的萨珊王朝，也就是在这个过程中，与东土大唐发生了――套用当年形容“美苏两霸”关系的词汇――“争夺”与“勾结”。 <br />
<br />
“争夺”的是对波斯及其以东地区的控制权。经过反复拉锯战，最後以高仙芝率领的唐军在751年的怛罗斯战役中败于阿拉伯帝国阿拔斯王朝的东征军，整个中亚伊斯兰化而告终。在此期间也有“勾结”。唐高宗永徽二年（651）大食国（阿拉伯）派遣的使节首次到达长安“朝贡”，标志著两国政治关系的确立，到贞元十四年间达三十七次之多。而民间往来早在此之前就已经非常密切。当时中国与阿拉伯之间有水陆两条交通线，路陆经波斯、阿富汗和西域，从甘肃到达长安；海路经波斯湾、阿拉伯海、孟加拉湾、马六甲海峡到达广州、泉州等地。根据《资治通鉴》记载，唐贞元三年时居留长安的“胡客”仅有田宅者就达四千人，其中主要是阿拉伯人和波斯人。扬州、泉州、广州、海南岛等沿海地区则聚集了许多从海路而来的“外商”。唐文宗实行开放政策，在大和八年（834）下诏说：“南海番舶，本以慕化而来，故在接以仁爱，使其感悦”，纠正当时“基层干部乱收费”的偏差。 <br />
<br />
这种开放国策，使得使节、商人、旅行家、航海家源源而来，他们在沿海城市的聚居区叫作“蕃坊”。以广州为例，外侨在唐政府专设的“蕃坊”内自行担任“蕃长”，实行自治管理。这些外侨中有伊斯兰教、犹太教、基督教（时称景教）、拜火教等教徒。他们非常富有，爱以烤全羊宴客；波斯妇女穿耳带环，有的多至20多枚。後世广州的广泛使用香药、斗鸡、蓄养黑奴、蛮婢等风俗，即来源于这个外商群体。有人估算唐代广州流动人口有八十万左右。 <br />
<br />
关于伊斯兰教传入东土的时间，史学界有各种说法。其实基本事实并没有争议，而是由于对“传入”的理解不同而产生的分歧。 <br />
<br />
陈垣先生认为，永徽二年大食国正式通使，即为回教传入中国之始。通使是政治关系而非宗教关系，但是，伊斯兰教传入中国不同于佛教和基督教的地方，就在于不是通过僧侣和教士，而是通过商业、军事、外交活动传播的。因此陈垣先生的观点得到很多人的采用。 <br />
<br />
如果“传入”指中国境内有许多回教徒，那麽此前就有不少。根据日本桑原藏《蒲寿庚传》记载，广州城内有怀圣寺，寺内有番塔，或称“光塔”，其构造与佛塔绝异。大殿形式类似穹庐，有西域风格，顶又是重檐歇山顶，形式与中国南方的杆栏式建筑相同。怀圣寺相传为隋代所建。清代金天柱《清真释疑补缉》内所收《天方圣教序》中提到，隋文帝听说“天方”出了“大圣”穆罕默德，就派遣使节到西域去“取经”，开皇七年，建怀圣寺，将取回的经典供奉在内。隋文帝在位时间（581-604）在穆罕默德创教（610）之前，此说并不可信，还是另一说此寺建于唐贞观元年（627），或开元二十九年（741），比较可信。 <br />
还有人说，回教徒作为外侨住在中国境内不算，还必须“认同”自己是“中国人”，才算“传入”。唐肃宗至德二年（757），朝廷为了镇压“安史之乱”（755-763年），曾向西方强国大食请求援助，哈里发当即派兵，协助唐军镇压了叛乱。安定之後，一部分大食军人返回本国，另一部分则居留在长安等地，娶妻生子，加入中国籍。根据这种标准，“安史之乱”平定後留下的大食“退伍军人”u是最早的中国穆斯林。 <br />
<br />
这几种说法虽然有正负一个世纪左右的误差，基本上都在公元八世纪前後，因此，一般认为，伊斯兰教在唐代传入中国。 <br />
<br />
（二） <br />
<br />
关于黄巢入广州的“灭回”事件，手边的周谷城著《中国通史》和钱穆的《国史大纲》里都没有，《资治通鉴》里也没有。仅见於吕思勉著《隋唐五代史》和韩国磐的《隋唐五代史纲》。此事的主要根据是十世纪的阿拉伯旅行家阿布・赛义德・哈桑在《中国印度记闻录》中的记载，说中国在874-901年间发生的黄巢农民起义军曾在攻入广州城时（878年）杀死回教徒、犹太教徒、拜火教徒等十二万人，其中回教徒有数万。另据来自西拉甫港的著名阿拉伯商人阿萨德声称：黄巢在广州杀死十二万人（又一说二十万人），其中大多数是来自东南亚、印度、波斯和阿拉伯世界的商人。数目虽然有出入，但杀人屠城，劫掠财货基本可以肯定是事实。 <br />
<br />
但是还有两点可以争议：第一，人是谁杀的？第二，屠杀是否属於宗教迫害？ <br />
<br />
关於第一点，中国大陆多年内的隋唐史权威韩国磐先生是这麽说的： <br />
<br />
或者说，据阿拉伯人阿布赛德哈撒（即上文所说旅行家，译名略有出入――引者）的记载，黄巢入广州，曾杀当地伊斯兰教徒阿拉伯、犹太等商人十二万以至二十万人，进入长安後也曾大肆杀掠。但这些都是当时与农民敌对的地主商人的记载，不能置信。他们往往将地主阶级军队残害人民的事，记在农民军的帐上，来歪曲历史。并且，当时国外豪商跟唐朝官僚勾结在一起，掠夺榨取劳动人民，所以义军入广州後，很可能杀掉一些和官吏一道剥削人民的外籍豪商，而剥削阶级的代表者因作过分夸张的记载，歪曲了事实。（《隋唐五代史大纲》，第289-90页） <br />
<br />
现在看来，韩先生为黄巢所作的辩护非常软弱无力。他既没有给出广州城Y的居民是官兵而不是黄巢义军所杀的证明，也证据说明死人数字为什麽是夸大的。吕思勉先生的《隋唐五代史》中关於黄巢入长安的记载也跟韩先生大同小异，说是把官军的杀人放火的帐记到了农民起义军头上，同样没有解释为什麽如此。（上册，第489页） <br />
<br />
官军当然也杀人（平民）抢劫，战争中胜利一方“大索三日”早就全球化了。但是，以韩先生所引韦庄诗《秦妇吟》来说，无论如何也是黄巢先到，官兵随後。韦诗云：“千闲仓兮万斯箱，黄巢过後犹残半，自从洛下屯师旅，日夜巡兵入村坞，匣中秋水拔青蛇，旗上高风吹白虎，入门下马若悬风，罄室倾囊如卷土。”官兵的抢劫杀人之“有”不能证明黄巢们之“无”，否则“残半”又从何谈起呢？ <br />
<br />
具体到广州，黄巢屠城的可能性更大。“若要官，杀人放火受招安”并不是小说，而是历史。乾伏三年（876），黄巢还跟王仙芝合夥的时候，蕲州刺史裴替王上表求官，朝廷授其为左神策军押牙，王准备接受，黄大怒，说“始者共立大誓，横行天下。今独取官而去，使此五千馀众，安所归乎？”还跟王打了一架，“伤首”，即把後者的脑袋打破了。但要从这件事说明黄巢的革命意志坚定，那就大错特错了，他是恨王“独取”，“官不及己”。王仙芝死後，黄巢本人也曾伸手要官，开价岭南节度使，朝廷方面认为“南海有市舶之利”，如为黄巢所占，则“国藏渐当废竭”，加以拒绝。黄巢闻讯大怒，攻入广州，抢劫杀人以愤，合乎情理。另外，黄巢跟其他农民起义领袖一样，打的是“劫富济贫”的旗号，并自称“天补平均大将军”，广州城Y聚居的外商既然非常富有，也就首当其冲成为刀下鬼。 <br />
<br />
关於第二点，基本上可以肯定不是宗教或种族原因。因为，黄巢起义前後，伊斯兰教在中国尚处在“初级阶段”，并没有被当作一个宗教，唐代史籍上将伊斯兰称为“大食法”，“特区”内实行。在唐武宗发动灭佛时（佛教徒称为“会昌法难”），封闭寺庙四千多所，勒令僧尼还俗二十六万多人，景教（基督教）、摩尼教、拜火教等也受到牵连，唯独伊斯兰教得以幸免。产生这种现象的原因，与伊斯兰教传入中国的特点有关。如上所说，伊斯兰教不是通过僧侣、教士传教，而是随著信仰伊斯兰教的使节、商人、士兵等在中国定居而“自然自在”的。这些人把伊斯兰教信仰作为内部的生活方式和风俗代代相传，不仅不热心传教，而且有意识地避免卷入激烈的社会斗争，因此，至少到唐末大乱时期为止，伊斯兰教的影响仍然很小。应该说，中国的早期穆斯林们非常明智地将信仰保持在团体内部，也正因为他们的明智，才获得了朝廷的优惠经商条件，在“法难”中得以保全。但是，躲得过初一，躲不过十五；跟“官”周旋成功，飘忽不定的“匪”一来，还是免不掉玉石具焚的命运。 <br />
人们常说，“历史是由胜利者书写的”，中文史籍对黄巢屠城事件记载的简略稀少似乎证明了这一点。黄巢起义虽然从根本上动摇了唐帝国的统治，毕竟被官军镇压下去，胜利者炫耀自身光荣的兴趣远远超过历数失败者的罪行。然而，在针对毫无抵抗力的平民的屠杀事件里并没有“胜利者”和“失败者”，只有“加害者”和“受害者”，古今中外的屠杀事件多半是认同或同情受害者一方记录的，加害者一方多用“战乱中难以避免”之类轻描淡写一笔带过。阿拉伯旅行家的记载成为广州屠城事件的主要依据，毋宁是顺理成章的事。 <br />
<br />
