																											<div id="postmessage_428613" class="t_msgfont">　所谓“四大发明”的无稽之谈<br />
作者飞虎队<br />
<br />
《所谓四大发明的无稽之谈》一文贴出后，很多人都指责我考据不细，论据不充分，论证不严密，逻辑不清。<br />
<br />
我于是把所有能够找得到的有关这方面的资料都深入查验了一遍，之后我不得不承认：我确实是考据不够细致，了解得不够深入；并且由于这种不细致，不深入，导致我错误地把注意力放在了一些次要的问题上，而忽略了一些主要的事实，使得某些人有空子可钻，将争论的方向转移向一些次要的方面，在一些逻辑问题上语言修辞问题上兜圈子，而有意无意地回避了一些重要的事实。<br />
　　<br />
　　特别是象技术含量，重要性这一类难以有可量化的标准来衡量的问题，更易于让人陷入圈套而纠缠不休，而且是无意中自设的圈套，而给予某些人继续其色厉内茬的指责以口实。<br />
　　<br />
　　如果说我在第一次谈到这个（四大发明）问题时，对自己的看法多少还抱着一点怀疑的态度，毕竟这是一个千百万人人云亦云了几十年的传言，多少会有一些道理吧。但是在我又多次刨根问底地挖掘了它的来源以后，我已经可以确信并肯定地说，所谓“四大发明”的说法，完全是一种虚假的宣传。<br />
　　<br />
　　我决定不再在逻辑问题上过多地兜圈子，也不再使用“也许”“可能”“我认为”之类模棱两可的语气，而是简明扼要地将一些基本事实罗列出来，然后明确地给出结论。<br />
　　<br />
　　下面就让我们来看一看（请耐心看完）。<br />
　　<br />
　　一、火药<br />
　　<br />
　　这是所谓“四大发明”的说法中最大的一个谎言。<br />
　　<br />
　　我在以前已经说过了，西方在谈到所谓的对历史前进起到了“重大作用”的三大发明（火药，罗盘，印刷术）时，根本不是在谈论所谓的我们中国人的“四大发明”，而是在谈论西方的“三大发明”。此火药非彼火药，此罗盘非彼罗盘，此印刷术非彼印刷术。<br />
　　<br />
　　而炮制并宣扬所谓的中国“四大发明”对历史作出了“重大贡献”种种说法的人们，最主要就是用偷梁换柱的手法，将两种性质截然不同并且没有任何关联的事物暗中偷换了位置，同时也转换了附着在两者身上的历史意义。其次就是用牵强附会，曲解，夸张，过滤等等手法来处理相关的历史事实，蓄意掩盖事实真相。<br />
　　<br />
　　就火药这一项，具体如下：<br />
　　<br />
　　（1）我们中国人所发明的“火药”实际上指的是“黑火药”，这种火药的用途，性能都有限的很。<br />
　　（2）而真正导致了军事变革并对历史起到了重大推动作用的“火药”是欧洲人发明的“黄火药”，并大规模地用于工程和军事，是整个近现代军事工业的奠基石。<br />
　　（3）黄火药跟黑火药之间完全没有任何传承关系。其一开始就是从一条独立的路线发展起来的性质完全不同的体系。（黄火药只是这个体系中几类产品的俗称，这个系列中的各种发明满足了现代工业和军事所需要的全部技术需求。）<br />
　　<br />
　　以上是最关键的三点，也就是有关“火药”这个谎言的核心之处，而更详细的历史细节如下：<br />
　　<br />
　　（4）黑火药的制造方法只是简单地将硝石，硫磺，木炭等几种天然物混和在一起，是一种混合物，属于物理范畴。而且只是知其然不知其所以然的感性认识，经验主义的积累。（所以在中国当然也不可能再有什么发展，这是毫不奇怪的）。<br />
　　（5）黄火药的制造方法是使用化学合成手段，是一种化合物。黄火药的产生发明出来是有近代化学科学作为理论基础的。（我以前说重要的发明往往其内在要有一定的科学理论作为支撑并没有说错，科学不发展到一定程度就是产生不出具有重大作用的发明）<br />
　　（6）而且，即便是黑火药，也没有任何证据能够证明是从古代中国传到西方去的，今天的种种说法，都只是猜测而已，而这种猜测仅仅只是依据不同发明在不同地区出现的时间早晚作出的，这被称之为文化上的一元论。（其他三项“发明”也是同样的这种情况）<br />
　　<br />
　　很多人会感到难以接受这种说法，有人也难免会这样说，那我们也可以不承认计算机，网络，原子弹这些发明是从西方传到中国来的。你可以这样说，只要你有本事不到别人的国家那里去留学而得到这些知识，并且完全不接触任何别国这方面的学术资料，那么没有人会否认这是你独立发明的。<br />
　　<br />
　　（7）更让某些人不愿提起的是，就连黑火药的起源也有多种说法，在很多古代文明地区都有类似于黑火药的发明的记载，学术界有一派的说法就认为，是印度人先发明了黑火药的。而黑火药的种类更有上百种之多。中国使用的黑火药只是其中之一。<br />
　　<br />
　　再来看黄火药的源流：<br />
　　<br />
　　（8）1771年，英国的P・沃尔夫合成了苦味酸，它是一种黄色结晶体，最初是作为黄色染料使用的（注意这一点），后来发现了它的爆炸功能，十九世纪被广泛用于军事，用来装填炮弹，这就是黄色火药的名称的由来。它是一种猛炸药。<br />
　　（9）1779年，英国化学家E・霍华德发明了雷汞，它是一种起爆药，用于配制火帽击发药和针刺药，也用于装填爆破用的雷管。<br />
　　（10）1807年，苏格兰人发明了以氯酸钾、硫、碳制成的第一种击发药。<br />
　　（11）1838年，T・J・佩卢兹首先发现棉花浸于硝酸后可爆炸。1845年德国化学家C・F・舍恩拜因将棉花浸于硝酸和硫酸混合液中，洗掉多余的酸液，发明出硝化纤维。<br />
　　（12）1860年，普鲁士军队的少校E・郐尔茨用硝化纤维制成枪、炮弹的发射药。俗称棉花火药。<br />
　　（13）1846年，意大利化学家A・索布雷把半份甘油滴入一份硝酸和两份浓硫酸混合液中而首次制得硝化甘油，硝化甘油是一种烈性液体炸药，轻微震动即会称列爆炸，危险性大，不宜生产。<br />
　　（14）1862年，瑞典的A・B・诺贝尔研究出了用“温热法”制造硝化甘油的安全生产方法，使之能够比较安全地成批生产。<br />
　　（15）1863年，J・威尔勃兰德发明出了梯恩梯（TNT）。梯恩梯的化学成份为三硝基甲苯，这是一种威力很强而又相当安全的炸药，即使被子弹击穿一般也不会燃烧和起爆。它在20世纪初开始广泛用于装填各种弹药和进行爆炸，逐渐取代了苦味酸。<br />
　　（16）1866年，A・B・诺贝尔用硅藻土吸收硝化甘油，发明出了达纳炸药。俗称黄色火药。<br />
　　（17）1872年，诺贝尔又在硝化甘油中加入硝化纤维，发明了一种树胶样的胶质炸药──胶质达纳炸药，这是世界上第一种双基炸药。<br />
　　（18）1884年，法国化学家、工程师P・维埃利首先发明了无烟火药。这一发明具有极重要的意义，为马克沁重机枪的发明提供了弹药方面的条件。<br />
　　（19）1887年，诺贝尔用硝化甘油代替乙醚和乙醇，也制成了类似的无烟火药。他还将将硝酸铵加入达纳炸药，代替部分硝化甘油，制成更加安全而廉价的“特种达纳炸药”。又称“特强黄色火药”。<br />
　　（20）1899年，德国人亨宁发明了黑索今，它是一种比梯恩梯威力更大的炸药。<br />
　　<br />
　　从上述线索可以清晰地看出，黄火药系列是怎样一步步独立发展起来并导致了近代军事的重大变革的。在这一过程中根本就没有黑火药什么事。黑火药在欧洲长期被用于烟火和纵火用途，也曾被用来作为枪炮的发射药，但是主要也就适用于中世纪的那种力量有限的原始火器，如火枪火铳，而中世纪火器跟近现代枪炮也完全是两种性质的概念，原理上，技术上，制造加工上都是完全不同的，不要被它们某些外形上的类似点所迷惑了。后面会具体谈到。<br />
　　<br />
　　注意到上面黄火药系列的发展源流，可以发现其中好几种重要的火药，最初被发明出来都不是准备要用于军事的，甚至有的最开始都不是要用来做炸药的。这个事实让某些打肿脸充胖子硬要厚着脸皮说黄火药的发明也是受了黑火药的启发的国人都无法自圆其谎了。更不用说黄火药跟黑火药在原理，性质，方法，用途等诸方面都是截然不同的。<br />
　　<br />
　　近现代整个军事工业体系中，从火帽底火所用的击发药，到雷管等用的起爆药，到弹筒中的发射药，弹体里装填的猛炸药，火箭导弹的推进剂，全部使用黄火药系列，根本没黑火药什么事，根本就是一种可有可无的东西，还有什么可吹的呢。<br />
　　<br />
　　黑火药现在也就在导火索、烟火药等方面还有点用途。仅靠使用黑火药根本就不可能产生出现代的这种后膛装填式射击武器和各种炸弹火箭导弹，这是被“四大发明”的鼓吹者们所故意忽略了的一个历史事实。<br />
　　<br />
　　有些国人常常用一种不无遗恨的口气说：古代中国人制造出了火药，却用来做鞭炮，太可惜了。在这里我可以明确地告诉你们，不是因为古人只想得到用黑火药做鞭炮，难道他们没有尝试过要把它用于军事上吗？而是因为黑火药也就只适合用来做鞭炮。不必为此感到遗憾了。<br />
　　<br />
　　如果说黑火药在中国是受了儒家文化环境的影响而没有发挥出应有的作用，那么在同样使用黑火药的日本，印度，阿拉伯，土耳其等等国家和地区，又产生了近代军事的重大变革作用了没有呢？没有。这种变革只有等到黄火药的出现才会产生。<br />
　　<br />
　　黑火药只是农业文明时代的一种原始产物，在历史上留下了一点痕迹而已，对推动工业文明的前进并无多大作用，有人却硬要把这点痕迹吹嘘到对历史起到了重大作用的程度，是很无聊也很无耻的。<br />
　　<br />
　　而伪古派分子在宣扬所谓“火药”推动了军事工业变革等等谎言的时候，却故意只字不提这是“黄火药”的功劳，用偷换概念的手法误导人们以为这是“黑火药”起的作用。这是一种非常无耻的作法。『也是很多“国学大师”“人文科学专家”炮制种种“中国古代文明成果”“科学贡献”的惯用手法。就是后面将会总结到的几种伎俩中的一种。』<br />
　　<br />
　　现在再来谈谈火器，本来这个问题已经没有讨论的必要了，但是我在《所谓》一文中，曾提到螺旋线膛枪，火帽，圆锥形子弹，撞针，连发式枪械等等发明的出现，以及弹道学，冶金学、金属加工技术等科学技术的理论基础是实现近代军事工业革命的决定因素，但是却忽略了另一方面，既弹药技术的发展所起的作用。<br />
　　<br />
　　来看看中世纪原始火器<br />
　　<br />
　　我在《所谓》一文中已经说得很清楚了，被穿凿附会地说成是最早的火枪的“突火枪”实质上只是一种喷射式火器，关于“子窠”到底是什么东西根本就是猜测，《宋史.兵志》上根本就没有具体说明，但是仍有人要胡搅蛮缠地把这个说成是最早的“火枪”。<br />
　　<br />
　　更无耻的是，有些人为了吹嘘这种“突火枪”，居然把《宋史.兵志》上记载的“声闻百五十余步”撒谎说成是射程达到了一百五十步！<br />
　　<br />
　　至于火铳，我看见过保存最早的元代铜火铳，它的形状就是一个长35.3厘米，口径10.5厘米的大圆桶，这是“枪”的概念吗？它这种管身长和口径的比例根本就不可能象现代火器那样发射弹丸来远距离杀伤敌人。如果它发射弹丸，那么弹丸直径起码要达到10厘米，其巨大的重量就根本不可能靠黑火药的力量发射出去，更不用说它还需要一定长度的膛内助推运动过程；如果它发射铁砂子的话，那么跟火药混在一起的铁砂子最多就只能喷射个几米而已，根本就只能起到一点威慑作用而已。中国的“考古学家”也就只能拿这些东西来蒙蒙外行人。<br />
　　<br />
　　（顺便说一句，某些自我感觉良好的人，常常给我抄录来一大段一大段的他自己都没看明白的文字，然后自以为是地“告诫”我多读点书再发言。我在这里明明白白告诉那些“业余历史学家”，只要是你读得到的书，别人也都能看得到的。千万不要卖弄自己读过多少书。）<br />
　　<br />
　　关于中西火器的源流<br />
　　<br />
　　（1）中世纪西欧的火枪与中国火枪完全没有任何技术上的相似点。完全是一种独立发展起来的火器。<br />
　　（2）十四世纪的意大利，产生了欧洲最早的火铳。欧洲现存最早的火铳是十四世纪中叶的。<br />
　　（2）1480年至1495年间，意大利科学家达・芬奇发明了轮发燧石枪机，用燧石的火花点燃火药池，再由火药池点燃火药，将弹丸发射出去。<br />
　　（3）最初的古代中国火枪，只是原始的金属管形火器。不仅在原理，技术上跟近现代枪炮完全是两码事，与同期欧洲的火器也不是一回事。已发现的最早火铳是元至顺三年（1332年）的。<br />
　　（4）根据《明史》的记载，十五世纪初，扳机击发式火绳枪开始传入中国，同时传入的还有佛朗机，密鲁铳，及后来的红夷炮，火炮瞄准具。十七世纪，开始有了燧石枪。<br />
　　（5）不管是东方西方，原始的中世纪火枪威力都有限的很，并没有对军事变革起到决定性的影响。<br />
　　<br />
　　再来大略看看跟黄火药同步发展起来的一些近代枪炮技术<br />
　　<br />
　　（1）最早进行爆炸式点火技术激发试验的是苏格兰人亚历山大・福希斯。最初使用器皿装雷粉，后来通把雷粉夹在两张纸之间而制成了纸卷“火帽”。<br />
　　（2）1808年，法国人包利应用纸火帽，并使用了针尖发火。<br />
　　（3）1814年，美国首先试验将击发药装于铁盂中用于枪械。1817年，美国人艾格把击发药压入铜盂中，发明了火帽，火帽的应用对后膛装填射击武器的发展具有十分重要的意义，并获得了迅速发展。<br />
　　（4）1821年，伯明翰的理查斯发明了一种使用纸火帽的“引爆弹”。后来，有人在长纸条或亚麻布上压装“爆弹”自动供弹，由击锤击发。<br />
　　（5）1840年，德国人德莱赛发明了针刺击发枪。其技术特征是：弹药从枪管后端装入，并用针击发火。<br />
　　（6）1860年，美国首先设计成功了13.2毫米机械式连珠枪，开始了弹夹的使用。<br />
　　<br />
　　某些人故意将中世纪的那种原始火器如“突火枪”之类的玩意跟近现代枪炮混为一谈，惯用的手段就是抓住两者之间仅有的一两点在外形上构造上的相似之处，如都是管形啊，都靠喷射气体推动发射物啊（根据这两点也可以说印地安人的吹管也是枪炮的前身），然后拼命吹嘘来蒙骗外行人，却故意的掩盖了两者在更多方面的根本不同，而且是导致两者功能上的巨大差异的构造上机理上的根本差别，如现代枪炮的后膛装弹，底火引爆，针刺击发，线膛枪管，圆锥形子弹，药筒装药，等等。没有这些根本特征根本就不成其为近代枪炮的概念。<br />
　　<br />
　　伪古派分子的这种手法就是指鹿为马的伎俩，好比说马有四条腿，鹿也有四条，所以根据仅有的这几点相似之处就可以说鹿就是马，而两者在其他本质方面的更多不同就根本不管了。『这就是鼓吹“四大发明”及诸如此类“古代科学发明”的人所惯用的手段，也是后面将会总结到的几种伎俩中的一种。』<br />
　　<br />
　　从我上面的论述中，已经可以清楚地看出：导致了近代军事革命并对历史起到了重大推动作用的“火药”是欧洲人发明的“黄火药”而不是“黑火药”。这是一个被蓄意误导了多年的虚假宣传，一个彻头彻尾的谎言。<br />
　　<br />
　　而且我以前说的重要的发明往往是要有发展到一定程度的科学理论作为基础并没有说错，科学不发展到一定程度就是产生不出具有重大作用的发明，从黄火药与黑火药的对比中已经可以清楚地看出来了。我以前说“四大发明”是弱智玩意也没有说错，至少对于黑火药（及与之相对应的原始火铳）来说就是如此，一种认识，只停留在感性阶段，而不能上升到理论高度，就是起不到什么大的作用和发展。<br />
　　<br />
　　谈到中国古代的科技发明时，某些人热衷于引用李约瑟的话来说明问题，挟洋以自重。我明确告诉你们，李约瑟的东西不足为证，他不仅不是搞科学史出身，而且没有得到国际学术界主流的认可，更是连汉语都不熟悉，这样搞出来的东西有多少可信度，难道翻古书，辨识文言文我们还不比他精通吗？而他书中的种种错误也早有人指出。中国人宣扬他只不过拉大旗做虎皮而已。我们将在后面详细讨论这个问题。<br />
　　<br />
　　二、印刷术<br />
　　<br />
　　这也是一个跟“火药”同样性质的谎言。通过偷换概念的手法将真正对历史起到了巨大推动作用的以古登堡发明的铅活字版机械印刷机和一整套印刷工艺为代表的近代印刷工艺技术换位为中世纪昙花一现的仍只是雏形状态的原始手工泥活字印刷术『偷梁换柱』。仅仅只是咬住了方法原理相差很远技术复杂程度更是不可相提并论的两种技术在都使用了活字这一点上的相似，而故意忽略了这两者在更多方面的不同『指鹿为马』，如一个是机械式，一个是原始手工操作；一个是合金浇铸的活字，一个是胶泥雕刻活字；一个使用脂肪性油墨，一个使用水墨，等等。<br />
　　<br />
　　先来看看西方的印刷术源流<br />
　　<br />
　　（1）首先要明确的是，古登堡发明的是什么，是『铅活字版机械印刷机』，这种垂直螺旋式手摇印刷机，是一个完整的机械体系。而不仅仅只是活字一个细节，使用活字只是这种印刷机技术中的一个环节。某些人故意把这一点夸大是因为原始的泥活字印刷术只有在这一点上跟古登堡发明的印刷术有相似处。<br />
　　（2）其次，古登堡的铅活字版机械印刷机完全是独立发明的，这一点是国际公认的。<br />
　　（3）古登堡的生平有详细的记载，关于他发明印刷机的过程也有详细的记载，“古登堡熟知制造硬币的钢模、印制扑克牌木刻版和铸造钟铃的字模方式来制成印刷版子，并且很快就察觉到每个字母必须要分开，且能移动，为能耐压则决不可用柔软的木材，而是坚硬的金属。”<br />
　　（4）1455年，古登堡发明了铅活字凸版机械印刷机，沿用了300年之久。这种印刷工艺技术是用铅、锑、锡三种金属按比例配比熔合而成，机器印刷，也称之为凸版印刷技术。<br />
　　（5）古登堡发明了一种手铸工具，将铜模放置其中，只要倾入溶化的合金，字母与符号即可产生，这种合金包含铅？<br />
　　（6）1812年，德国的柯尼希发明了蒸气动力印刷机；1847年，美国的霍伊发明了转轮印刷机；1863年美国人布洛克发明了滚筒印刷机。1900年，制成六色轮转印刷机；1904年，美国的鲁贝尔发明胶版印刷机。这些由机械操作的印刷机，使作为大众传播媒体的报纸得以大量印刷，从而真正地拉开了大众传播的序幕，具有划时代的意义。<br />
　　<br />
　　不要以为欧洲的印刷技术发展就是发明了铅活版印刷术那么简单，后面更多的技术革新和发明更是与那种农业文明时代的原始印刷术毫无干系了，这当然是“中国发明了印刷术”的鼓吹者无论如何都不会提起的罗。<br />
　　<br />
　　（1）凸版印刷技术，是用图文部分高于空白部分的凸版进行印刷的工艺技术，中国传统的雕版印刷术，泥活字印刷术就属于这一类型。凸版印刷技术不仅包括铅活字版印刷，还有以铅活字版为母版的泥版翻铸铅版印刷、纸型翻铸铅版印刷和照相铜锌版印刷。照相铜锌版是凸版印刷术的进一步发展，是照相术应用于印刷制版而出现的的发明和应用。<br />
　　<br />
　　（2）凸版印刷机械，主要包括凸版制版机械和凸版印刷机械两部分。其中，凸版制版机械主要是用于活字制作的铸字机械和用于翻制复制版的压型机，以及照相术用于凸版制版后出现的制版照相设备；凸版印刷机械则主要是各种凸版印刷机。<br />
　　<br />
　　（3）平版印刷技术。平版印刷机印版表面的图文部分与空白部分几乎处在同一平面上。它利用水、油相斥的原理，使图文部分抗水亲油，空白部分抗油亲水而不沾油墨，在压力作用下使着墨部分的油墨转移到印刷物表面，从而完成印刷过程。<br />
　　<br />
　　（4）凹版印刷技术。凹版印刷的主要特点是印版上的图文部分凹下，空白部分凸起，与凸版印刷机的版面结构恰好相反。机器在印单色时，先把印版浸在油墨槽中滚动，整个印版表面遂涂满油墨层。然后，将印版表面属于空白部分的油墨层刮掉，凸起部分形成空白，而凹进部分则填满油墨，凹进越深的地方油墨层也越厚。机器通过压力作用把凹进部分的油墨转移到印刷物上，从而获得印刷品。<br />
　　<br />
　　（5）丝网印刷技术。丝网印刷机属于孔版印刷机中较有代表性的印刷设备，它的印版是一张由真丝等材料编织而成的纵横交错、经纬分明的丝网。已经生产的有平面、曲面、成形、印染、印刷电路和新型轮转等多种丝网印刷机。其中，新型轮转丝网印刷机的速度和生产率比较高。它的特点是将丝网安装在滚筒上，油墨浇在滚筒内。机器工作时滚筒旋转，橡皮刮墨刀把图案快速印到印刷物上。<br />
　　<br />
　　再来看看原始的泥活字印刷术。<br />
　　<br />
　　（1）考古学历史学最讲的是证据，特别是实物证据，而且孤证不足为信。恰恰这个毕升发明泥活字印刷的说法，既没有他的泥活字留传，也没有任何印本存在，而且关于他和他的泥活字印刷，除了《梦溪笔谈》上简略的记叙外就没有任何其他记载了。中国学界整天拿来唠唠叨叨的就是这么一个没有任何实物依据的孤证。<br />
　　<br />
　　（2）而且到底有没有毕升这个人都是个问题，他的生平，籍贯等等都没有任何记载。（有好事者还发掘出了据说是他的墓碑，可是根本无法证明这个连身份籍贯都没有任何记载的毕升是碑上那个名字代表的人，反正先把它“初步认定为是”嘛，不仅又多了一点可吹嘘的资本，而且隔三岔五地为这块莫名其妙的碑石开开研讨会，借机又可以公款旅游一番。造假成性的中国“考古学界”）<br />
　　<br />
　　（3）沈括所记的毕升泥活字印刷术，后来也不是没有人效仿过，清代翟金生等人就仿制过，为此居然花了三十年功夫才印成第一本书籍，可见这种东西效率之低下，毕升的泥活字印刷技术远未成熟，根本就没有在现实中得到什么应用，从宋元到明清普遍使用的都是雕版印刷。又何来“对人类历史作出了重大贡献”云云？<br />
　　<br />
　　（4）毕升的泥活字印刷术连他那个村（镇？谁知道他是哪里的）的范围都没有传出去，却莫名其妙凭空地一下子就传到了数万里外的其他国家去了。硬要强词夺理地说别人的东西是从我们这里传过去的，简直是无赖。<br />
　　<br />
　　（5）木活字也有人尝试过，不过效果都不理想，而使用过一次的木活字很难拆卸、损坏率很高，印墨也不均匀。被认为是首创了木活字的元代王祯也没有印本实物，而现存的西夏文木活字印本又比他年代早，那到底是从中原传到了西域，还是从西域传进了中原？<br />
　　<br />
　　（6）而且中国的墨是水墨，不适合于金属版的印刷。因为墨汁不易均匀附着在金属版面上，印刷出来质量欠佳，金属版印刷也不成功。<br />
　　<br />
　　（7）另外，国际上现在公认韩国清州兴德寺是最早使用金属活字（铜活字）的地方。其铜活字印本《佛祖直指心体要节》（1377）比德国古登堡铅印版的《圣经》还早70年，被联合国教科文组织认定为世界最古老的金属活字印本。被指定为联合国教科文组织世界纪录遗产。因为别人拿得出实物证据，你不服也没办法。<br />
　　<br />
　　（8）如果一定要把古代农业文明时代的任何一点点跟工业文明时代真正意义上的现代印刷技术有某些相似点的原始萌芽技术都指鹿为马地算作是“发明”了印刷术的话，那也轮不到他毕升头上，因为这样的原始印刷术雏形在世界各地区都有所发现，比毕升早两千多年的古希腊克里特岛米诺斯文明遗迹中就已经发掘出了最早的活字印品。（注：这是世界上最早的活字印刷，将许多个活字印拼在一起，印在泥版上和陶器上，克里特岛出土了大量这样的活字印和泥版及陶印品）<br />
　　<br />
　　有人辩解说，活字印刷术在中国没有普及开来，是因为中国字多，要刻很多字，而外国字字母少，所以便利。这种说法看上去好像有道理，仔细一推敲却根本站不住脚。难道用拉丁文印刷书籍就只需要用那二十多个铅活字就行了吗？每一页还不是一样要成百上千个活字，古登堡发明铅活版印刷机后，印刷需求量大增，一本书很多页都同时装版印刷，需要的活字一样数以万计。而且同一时期，在最早使用金属活字的韩国清州为了印刷曾经制造出上百万的铜活字。字多字少根本不是问题。<br />
　　<br />
　　有人根据沈括的记载里有这样一句话“若止印二三本，未为简易，若印数十百千本，则极为神速”而硬要说毕升的泥活字印刷在现实中得到了广泛应用。这句话哪里说出了这么个意思？只要认识汉字的人都知道那个“若”是“如果”的意思。而且随便那个人看见了毕升那种排好了的版也会得出这么个印象。“若印数十百千本”到底是数十本还是数百本还是数千本？？明明是虚指，中国的这些“国学家”硬要把这句话歪曲成是事实。这些也就只能蒙蒙不懂文言文的老外。<br />
　　<br />
　　（9）关于雕版印刷术，我在《所谓》一文中也已经说得很清楚了，那是跟印章性质原理一样的东西，世界上很多国家民族早就有了。在埃及发掘出来的印在纸草纸，羊皮纸上的雕版印刷品，时间上至公元900年。何况“四大发明”的说法指的本来也只是活字印刷。<br />
　　<br />
　　（10）即便是雕版印刷术，也没有任何证据能够证明是从古代中国传到西方去的，今天的种种说法，都只是猜测而已，而这种猜测仅仅只是依据不同发明在不同地区出现的时间早晚作出的，这被称之为文化上的一元论。<br />
　　<br />
　　（11）在蒙古人西征之前，中国跟欧洲基本上处于隔绝状态。几乎没有任何文化上的交流。（注：所谓的“丝绸之路”，最远只到达了现在的伊朗）<br />
　　<br />
　　（12）而说蒙古人西征把中国的印刷术带到了西方去的说法也是根本没有任何道理的，因为当时蒙古人尚未达到高度的文明，而且对欧洲的进攻都是突然袭击的性质，没有与当地人民进行什么文化交流，这一点早就有人指出来了。欧洲人描述蒙古人就是突然来了又突然消失了。<br />
　　<br />
　　（13）那以后马可・波罗在他的游记里，也没有提到过书籍印刷的事情。<br />
　　<br />
　　（14）位于远东和欧洲之间的阿拉伯世界，一直拒绝使用印刷术，坚持用手抄写书籍，一直到十九世纪末，更不可能成为什么“传播印刷术的桥梁”之类的。<br />
　　<br />
　　中国学界找不到任何证据来证明“欧洲的印刷术是从中国传过去的”这种说法，相反的证据倒有不少，但是还是要厚着脸皮说“总而言之，欧洲发明印刷术间接地受到了中国的影响”。真是好笑。<br />
　　<br />
　　实际上，又有谁会相信两千多年前古希腊时代就已经发展出了精密科学，拥有苏格拉底，柏拉图，亚里斯多德，阿基米德，欧几里德，这样的智慧的古代西方人会想不到像印章那样把刻在木头上石头上泥板上金属上的文字符号翻过来印在另外的东西上？能够想得出几何学，牛顿力学，相对论的脑袋瓜子还用的着别人来教他们才会刻泥巴字？只不过因为那时的印刷材料有限，雕版印刷术普及不开来而已。<br />
　　<br />
　　有个网友说得好：我小时候还不识字的那会，就知道在泥巴上刻上些小花纹，蘸点水在地上印着玩了，有什么稀奇嘛，谁想不到吗。能在实践中得到有效而广泛应用的东西才能算是发明。不然最多也就只能算是失败的发明。我认为这话很有道理，明明古埃及人早就发明了纸，也长期在西方得到应用，可有些人偏要说古埃及人发明的纸跟中国的纸方法不太一样，而且现在使用的是中国的纸而不是埃及的纸，所以那不算。可是现在使用的是黄火药而不是黑火药，使用的是古登堡的铅活版机械印刷机而不是手工的泥活字印刷，方法原理也完全不同，那为什么又要算在自己头上。真是不要脸。<br />
　　<br />
　　不仅欧洲，而且整个世界包括中国在内，都是在古登堡发明了铅活版机械印刷术并逐渐传播到世界各地以后才开始了信息传播的革命性改变。中国也是在近代引进了铅活版机械印刷术才抛弃了那种原始落后的手工雕版印刷术并产生了印刷业的根本改变的。这是一个历史常识。中国的那些“历史学家”敢否认这一点吗？不是印刷术从中国传到了西方去，而恰恰相反，我们现在真正使用的印刷技术倒是从西方传过来的。<br />
　　<br />
　　实际上，国际学术界普遍把古登堡发明铅活版机械印刷机算作是现代印刷术的历史起点，没有谁把中国发明印刷术这种说法当真。<br />
　　<br />
　　三、罗盘<br />
　　<br />
　　有人可能会说，这个应该没有什么问题了吧？可惜得很，指南针的宣传中同样包含了很多的虚假成分。下面就来看看。<br />
　　<br />
　　（1）关于司南，我在《所谓》一文中已经谈到过了，根本就没有任何详细的记载，也没有任何实物被发现。考古学家甚至都无法知道它的准确形状。我们今天所看到的，只不过是一个根据野史杂书中一些只言片语的描述推断想象出来的模型而已。有些人就指责我，为什么要拿这个暧昧的“司南”来作比较，为什么不拿更有说服力的“指南车”“指南针”来作比较。那我们就具体地分别来看看。<br />
　　<br />
　　（2）指南车跟指南针不是一回事，它运用的不是磁原理。而且这个指南车根本就只是传说。历史博物馆陈列的那个所谓“复原”出来的模型实际上是根据现代技术炮制出来的伪文物。这个道理就好比先用现代科技造出一台汽车，然后给它套上古代马车的外壳，然后就可以声称：看，我们早在几千年前就已经发明了汽车！【这一招叫做无中生有，也是伪古派分子惯用的伎俩之一，后面再来总结】<br />
　　<br />
　　（3）然后再来说指南针，这里面同样存在着一个偷梁换柱的问题。因为罗盘（指南针）也分两种：水罗盘和旱罗盘。我们中国人发明并使用的是水罗盘（现在也还有这种漂浮式罗盘），而欧洲人发明的是旱罗盘，而我国到了明朝嘉靖年间时才引进旱罗盘。<br />
　　<br />
　　（4）水罗盘是通过在漂浮在一碗水里的木制圆盘上安装一块磁铁来作用的，这种水罗盘使用很不方便，中国长期使用水罗盘却没有得到改进。（有人硬要把“张仙人”俑上那个模糊不清的圆疙瘩牵强附会地说成是旱罗盘，也就只能蒙蒙没看到实物的人）<br />
　　<br />
　　（5）欧洲人的磁罗盘却是12世纪时在意大利的阿马尔菲城自己发明的。它通常包括在多个枢轴上水平安装或悬挂的磁针，可在枢轴上自由活动直到与地球磁场在一条线上，在技术上要复杂先进得多。这个有出土的实物为证。<br />
　　<br />
　　（6）即使是水罗盘，也没有任何证据能够证明是从古代中国传到了西方去，今天的种种说法，都只是猜测而已，而这种猜测仅仅只是依据不同发明在不同地区出现的时间早晚作出的，这被称之为文化上的一元论。<br />
　　<br />
　　（7）欧洲人不仅在技术上不断革新，而且在理论上也进行了深入的探讨，1269年，柏尔格利纳斯（P.Peregrinus）写出了《磁论》一书，对磁极、磁针、磁力和地磁的科学原理进行了仔细的实验研究。这以后，欧洲人又对罗盘进行了很多技术革新，如加上了防磁干扰的弗林德斯顿铁等装置。<br />
　　<br />
　　（8）而罗盘的种类也早就从磁罗盘一种发展到了跟磁原理毫无关系的陀螺罗盘，无线电罗盘等等多种。这还有我们中国人什么事呢？硬要厚着脸皮把别人的东西说成是自己的不觉得可耻吗？<br />
　　<br />
　　（8）关于磁罗盘在航海中的重要性，本来这个问题已经没有讨论的必要了，因为人家用的是自己发明的东西，重不重要都不关你的事。但是有些问题还是需要澄清一下。我以前已经说过了，航海所需要用到的仪器装置多得多，用来确定方位的仪器和方法也远不止一种，磁罗盘只是进行航海所用到的众多手段中的一种而已，只能看个大概，而六分仪不仅可以确定方向，还可以确定自己在地球上的位置。远洋航行中六分仪更加重要。<br />
　　<br />
　　（9）有人认为，近岸航行跟横渡大洋不能相提并论，所以公元前七世纪的腓尼基人环航非洲算不得什么。这种想法简直太书呆子气了，难道近岸航行真的就是像你想象的那样沿着看得到陆地的浅滩航行吗？那还航行个屁啊，早就触礁了，没怎么出过海的人才会是这种想法。所谓近岸航行一样是在远离陆地的远海航行。更何况非洲大陆沿岸到了十八十九世纪都还是蛮荒一片，更不说公元前七世纪，要环航非洲根本就连个靠岸补给的港口都没有，要这样航行数万公里的艰难程度岂是你两千年后的郑和下西洋能够相比的。话又说回来了，郑和下西洋还不是近岸航行。<br />
　　<br />
　　（10）如果说环航非洲是近岸航行，那么古埃及人希腊人罗马人腓尼基人阿拉伯人横渡地中海阿拉伯海也是近岸航行吗？公元前两千年前米诺斯文明时期的希腊航海业就已经很发达了，腓尼基人，米诺斯人一直就靠航海为业。迷航？走在马路上还摔跤呢。<br />
　　<br />
　　不管从那方面来看，发明权上，技术成熟程度上，科学理论的建立上，实践应用上，对航海的推动作用上，都没有理由硬要说别人的发明跟我们有什么关联，我们的是我们的，人家的是人家的。<br />
　　<br />
　　四、造纸术<br />
　　<br />
　　这是“四大发明”中唯一还有点可骄傲成分的一个。可是国际上偏偏不承认是中国人最先发明了纸，而是认为五千多年前的古埃及人最早发明了纸。有人就说，古埃及人发明的纸不能算真正的纸，因为不是植物纤维的纸，有人甚至说纸草纸是跟甲骨，竹简同样的东西。这不是蛮不讲理吗？纸草纸怎么就不是植物纤维的纸？又怎么会是跟甲骨，竹简同样的东西？甲骨竹简能任意折叠吗？是用笔书写吗？<br />
　　<br />
　　我在《所谓》一文中都说得很清楚了：这种用纸草纸写成的书籍，我曾经亲眼看到过，虽历经数千年，仍然清晰如新，纸质决不比我们中国古代的纸差，而且纸草纸也不像有人说的那样，容易断裂，现在埃及都还有根据古代技术进行制造的纸草纸，造出的纸任意折叠完全没有问题。世界上很多博物馆都收藏有这种纸草纸古籍，像大英博物馆就收藏有，我叫你们去看，你们又不去，还硬要说纸草纸不算纸。<br />
　　<br />
　　先来看看纸草纸的制造工艺：<br />
　　<br />
　　“台旁一个水桶中插着一把1米来长的绿色纸草茎，很像芦苇杆，但顶上长着稀疏的线状短叶。这些都是野生的，割下后要浸在水中，使用时间不能超过两天。青年取出一根纸草，割下三四十厘米长一段，用小刀将外面绿色的硬皮削去，露出浅色的内茎，又将它切成一片片薄片。但那时的薄片很脆，用手一折就断，而一个小水槽中浸泡着的薄片颜色已变为浅黄，拿在手里可以任意弯曲。他告诉我们，由于新鲜的纸草茎中含有糖分，必须浸泡6天，才能消除糖分，成为造纸原料。接着他表演第二道工序，从水槽中捞出浸透了的薄片，先用木棰敲平整，再用一根像擀面杖一样的圆棍挤尽水分，然后将这些薄片纵横交错的叠成一片。最后一道工序是将它放在一台铁板压中压紧，放置满6天后就成为纸草纸成品。他将叠在一起的薄片放入压机后，将铁板转紧，两分钟后取出薄片，但见相互间已经粘在一起。可以想象，6天后还是什么情形。果然，接过他取来的一片成品，用两手抖动就哗哗作响，可以随意折叠，透过光照，可以看到纵横的纤维，厚薄虽不太均匀，薄处成半透明，但已浑然一体。不过，古埃及人制作时还没有这些机械，特别是最后一道工序，当时是将薄片平摊在两层亚麻布中间，然后用石头或其他重物压紧，需要的时间更久。”<br />
　　<br />
　　确实，这是跟我们中国的纸制造工艺有所不同，因为还没有发展到造纸浆的阶段，而且后来也逐渐淘汰了。但是我们并不能否认纸草纸在性能上是属于纸的类型的，而不是跟甲骨竹简同一类的，我们中国人不是也把丝绵纸看作是纸的一类吗？更不能否认这种纸在古代及中世纪欧洲和中东的长期使用对文化发展传播所作出过的巨大贡献。（注：英文中纸（paper)这个单词就直接来源于拉丁文papyrus（纸草纸））<br />
　　<br />
　　纸草纸最大的缺点就是原料单一，纸莎草只生长在尼罗河畔，来源单一导致了造价昂贵，后来逐渐就被淘汰了。<br />
　　<br />
　　那么后来欧洲所使用的造纸工艺是从中国传入的吗？遗憾的是也没有证据能够证明这一点，仍然只是猜测，认为是阿拉伯人传入的。欧洲的造纸术跟中国的造纸术有很多的不同，中国传统造纸术最初使用树皮，破布，麻头等等，后来长期使用竹纸，欧洲的竹子少，十九世纪开始广泛使用木材造纸，他们用的抄纸工具也不是竹帘，而是用木头和铜丝等作成的“抄网”，这与中国人单人掌帘或者双人抬帘是不同的，他们在纸浆中不加纤维悬浮剂，所以欧洲的纸张比较厚实和光滑，主要为了适应鹅毛笔和染料墨水书写的结果。德国和法国又于十八世纪试用各种植物纤维进行纸浆造纸的实验，英国采用机器代替手工抄纸，以木材为原料的机制纸也得到了迅速发展，造纸工艺早就不是传统的那种石灰沤竹的土造纸法了，产生了巨大的变革，中国今天使用的倒是西方的机造木材纸工艺。整天唠唠叨叨我们的造纸术为你们作出了巨大的贡献实在是无聊也夸大了事实。（注：现在德国等地的很多小镇上仍然有纪念发明造纸术和印刷术的各种节日，中世纪时，这些地方曾经大量地将树木浸泡在河水中，用来造纸。）<br />
　　<br />
　　有的时候我会想，咱们中国人怎么就那么在意我们以前是不是第一，不惜撒谎吹牛都要去维护这个第一，却不关心我们现在是不是第一，这是不是一种病态的心理呢？就像一个人，如果生活事业顺利，就很少去回忆过去的事情，也没有时间去回忆。只有失败者，对前途绝望者，对生活失去信心者，才老怀念过去，靠这个来安慰自己。或者是像人老了就喜欢怀念过去，年轻的就喜欢幻想未来。现在中国的复古风气很盛，可能我们中国人真的是觉得已经快走到穷途末路了吧。<br />
　　<br />
　　有一件事情或许可以解释这个问题。<br />
　　<br />
　　“有一位留学生朋友曾经和一个美国旅游团搭同一架飞机，快到北京国际机场时一个美国导游对他们的美国同胞说‘各位，下一站就是中国的北京，这个国家以前被侵略过，人们有一种屈辱感，这个国家的人民有个特点，非常在意别人夸他们天下第一，等一下大家去玩的时候，不妨对他们国家的人民翘起大拇指，并说他们的国家天下第一，他们听了会很高兴，无论看到什么东西都夸下，说这是个奇迹，他们更会欣喜若狂，并更加热情好客’”<br />
　　<br />
　　其实，国际上并没有否认中国人自己【独立】发明了黑火药，雕版印刷术，造纸术，指南针，但是指出来你发明了别人也发明了，而且你发明的跟别人发明的不是一回事，更何况现在使用的却是别人发明的东西，用的是黄火药而不是黑火药，用铅版机械印刷机而不是手工的雕版印刷，用旱罗盘，无线电罗盘，陀螺罗盘，卫星定位仪而不是原始的水罗盘，用机造木材纸而不是石灰沤竹的土造纸法。我们中国人也明白这一点，于是就死皮赖脸地硬要把别人的东西说成是自己的，真是既无聊又无耻。<br />
　　<br />
　　关于李约瑟的问题，《南方周末》上有一篇文章就已经讲的很清楚了，以下就直接引述这篇文章中的观点：<br />
　　<br />
　　“在西方“正统”科学史家──从“科学史之父”萨顿一脉承传──中的某些人看来，李约瑟不是“科班出身”，而是“半路出家”的，还不能算是科学史家共同体中的一员。李约瑟还曾抱怨：剑桥东方学院的学者不同他打交道，“一墙之隔”的剑桥大学科学史系也把他拒之门外。”<br />
　　<br />
　　“李约瑟虽然在生物化学方面早有成就，37岁时便成为英国皇家学会会员，但他并未受过科学史或科学哲学的专业训练，因此，未能“把什么是科学加以定义”成为李约瑟的一大困境。而实际上，李约瑟有时拔高古代中国人的成就，也和不对科学加以界定有关系。”<br />
　　<br />
　　“而李约瑟的“硬伤”更具体的根源是他对中国道教及道家学说的过分热爱──热爱到了妨碍他进行客观研究、夸大道家在中国科技史中作用的地步。”<br />
　　<br />
　　“对于李约瑟研究中国科学技术史的工作本身，海内外许多学者曾指出各种具体错误，李约瑟出于对中国传统文明的热爱和迷恋，在不少问题上有对中国古代成就的拔高倾向。”<br />
　　<br />
　　“从他和鲁桂珍相遇开始──有些学者还注意到当时鲁年轻貌美，此后他的思想和兴趣发生了巨大转变。鲁后来成为他终身的生活和工作伴侣，最终成为他的第二任妻子。他在《李约瑟文集》中文版序言中说：“后来我发生了信仰上的皈依（conversion），我深思熟虑地用了这个词，因为颇有点象圣保罗在去大马士革的路上发生的皈依那样，……命运使我以一种特殊的方式皈依到中国文化价值和中国文明这方面来。”<br />
　　<br />
　　“真正的持平之论，出自李约瑟身边最亲近的人。鲁桂珍曾坦言：李约瑟并不是一位职业汉学家，也不是一位历史学家。他不曾受过学校的汉语和科学史的正规教育。实际上他根本没有正式学过科学史，只是在埋头实验工作之余，顺便涉猎而已。”<br />
　　<br />
　　“在特定的历史背景下，中国“选中”了李约瑟，但对他的工作成果进行“过滤”。李约瑟著作中提到的中西方之间的交流、影响和“西来说”都为国内许多学者所不喜爱--他们通常只字不提，只当李约瑟没说过。有的人士更是只挑选对自己有利的结论加以引用，少数学者甚至严重歪曲李约瑟的观点来证明自己说法。”（如中药相当一部分就是当年穆斯林商人与医药学家从阿拉伯、波斯与印度等地引进的“海药”，16卷本的古希腊医学家盖伦医学著作传入中国对中医的影响，这些都隐而不提了。）<br />
　　<br />
　　“罗伯特・坦普尔著书谈“中国的一百个世界第一”，有很多穿凿附会的地方。”<br />
　　<br />
　　“那种认为中国科学技术在很长时间里世界领先的图景，相当大程度上是虚构出来的──事实上西方人走着另一条路，而在后面并没有人跟着走的情况下，中国“领先”又从何说起呢？这就好比一个人向东走，一个人向南走，你不能说向南走的人是落后还是领先向东走的人──只有两个人在同一条路上，并且向同一个方向走，才会有领先和落后之分。”<br />
　　<br />
　　（《南方周末》2001.5.24.“李约瑟难题”是伪问题？──《南方周末》专访）<br />
　　<br />
　　如果说李约瑟的著作多少还有点参考价值的话，那么那个靠贩卖畅销书为生的美国记者<br />
　　罗伯特・坦普尔写的《中国--发现和发明的国度》简直就是一本跟拉丰丹的《众神之车》毫无二致的东西。<br />
　　<br />
　　随便列举一下他贩卖（以及帮他叫卖的中国伪古派人士更进一步发扬光大的）的这些东西：二进制，明明是莱布尼兹创立的东西能够被他这个连文言文都不懂的洋风水师说成是易经的发明；“发烛”，明明只是涂了硫磺用来在现成的火源上引火而根本不具备火柴的自发生火基本功能性质的东西，能够被无中生有地撒谎说成是发明了“火柴”；“机械钟”，能够牵强附会地说成是中国的发明，而实际上，机械钟表早在古希腊时代就已经有了；“记里鼓车”，指鹿为马地说是古代计程车，而实际上，公元前100年，亚历山大里亚的发明家赫伦就已经发明了类似的里程计，在里程计中使用了齿轮。还有风筝，伞等物，实际上别的国家民族也早就有了类似的东西，如古埃及人早就开始使用伞了，风筝，最早的风筝发掘于公元前一千多年前的南太平洋岛上，公元五世纪时，希腊的阿尔克达新也发明了风筝。弓箭，也成了中国人的独家专利了，还弱智到撒谎说欧洲直到十世纪才开始使用弓箭，那之前两千年里古希腊人古罗马人没使用过弓箭啊？亚历山大的攻城弩是什么？几万年前的原始部落就有弓箭了。蒸气机也说成是中国发明的了，实际上按照同样的标准来衡量（如果合理的话），也可以说古希腊人早就发明了蒸气机。<br />
　　<br />
　　【这也是伪古派分子惯用的一种骗术，先用偷梁换柱指鹿为马甚至是无中生有等等手段炮制出或者是把一些原始技术伪装替换成某种现代科技的等价物，然后故意隐瞒其他国家民族在古代同样也早就有了类似的技术这样一个事实，结果给人造成这样一种印象：原来我们一直领先他们几千年啊！其实是在梦中领先了几千年。】<br />
　　<br />
　　还有什么“古代机器人”“古代直升飞机”之类的根据中世纪版《转.法.轮》胡编乱造出来的玩意；“化学武器”“催泪弹”“降落伞”“塑料”“火箭”“水雷”之类指鹿为马的胡说八道；“眼镜”“国际象棋”“扑克牌”之类偷换概念不着边际的歪曲夸大。<br />
　　<br />
　　就是这个罗伯特・坦普尔（如果不是同名同姓的话），他还论证过阿基米德是激光武器的发明者，根据的就是那个阿基米德用镜子反射阳光烧毁了罗马舰船的传说。这些洋江湖术士每到一个国家都会用同样的手段炮制出类似的一些“伟大发明”的神话出来。<br />
　　<br />
　　这种招摇撞骗的洋瘪三也就只有在中国才特别受欢迎，帮着他贩卖这些垃圾的“国学大师”“科学史专家”并不是不清楚他是在胡说八道，只不过互相利用而已，一个靠贩卖畅销书骗钱，一些靠帮着贩卖混口饭吃，再用这些来愚弄老百姓，一方面又迎合了“主旋律”。<br />
　　<br />
　　官方热衷于支持操纵这种复古伪古热潮，其动机不过是为了要向人民证明，没有西方的政治体制，我们中国古代也是科技发达领先的，现在我们也仍然不需要引进西方体制，如此而已。<br />
　　<br />
　　下面来总结一下，伪古派分子编造种种古代“科学神话”所惯用的伎俩，以后大家看见类似的跳大神表演就便于识别了。<br />
　　<br />
　　（1）无中生有，根据一些古代传说甚至干脆就是神话故事，封建迷信的谣言，还有类似于《搜神记》《易经》之类的古代版《转.法.轮》古代版《众神之车》中只言片语的碎片，甚至是像《史记》《本草纲目》这样严肃著作中也难免的封建迷信糟粕成分（如关于刘邦的记叙），再加上文言文的语义晦涩，多歧义，通过曲解，穿凿附会，甚至干脆就是故意撒谎，来编造所谓的“古代科学发明”；<br />
　　<br />
　　（2）指鹿为马，根据一些农业文明时代的原始手工工艺技术与现代的某种科学技术在某方面的一点点相似之处，然后就无限夸大这种原始技术的作用和功能，故意掩盖两者在更多方面的巨大差别，来编造谎言。<br />
　　<br />
　　（3）偷梁换柱，干脆就把两种不同性质不同功能的东西偷换概念，将另外一种真正起作用的东西隐而不提，而虚假地宣传本来无关的东西的作用。<br />
　　<br />
　　所谓“四大发明对人类文明作出了巨大贡献”云云，其实就是用这样种种手段炮制出来的一种虚假的宣传，一种在病态的无限膨胀的虚荣心的驱使下被蓄意制造出来的谎言，一个彻头彻尾的谎言，我不指望凭着几次讨论就能够结束这种虚假的宣传，但是我相信会有越来越多的人不再相信这些谎言。 <br />
　　<br />
