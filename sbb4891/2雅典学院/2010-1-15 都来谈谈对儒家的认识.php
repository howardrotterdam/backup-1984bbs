													<h2>秦晖：儒学复兴根本在于谁有权掌握评判标准</h2>
						
						
																											<div id="postmessage_434479" class="t_msgfont">秦晖：其实谈儒学，谈国学，我觉得谈什么都可以，但是你要讲浴火重生，似乎儒学以前已经死了。有一个问题，就是它什么时候死了？当然了，我觉得传统意义上的儒学秦朝之后就已经死了。传统儒学是以三代为摹本的，在春秋战国这个时期应该说它是代表了对整个变化非常不满的，所以这种不满从正面的意义上，你可以说这是一种社会批判，甚至代表着有些人说的知识分子的良心，如果从那些否定他的人就说，那是什么复古倒退，逆历史潮流而动。但不管怎么说，正因为当时的儒学基本上是一个歌功颂德比较少，那种骂骂咧咧的话讲的比较多，所以后来就很不受欢迎，一直到秦的时候就整个给灭掉了。 <br />
<br />
秦晖：当然不仅仅是焚书坑儒了，把诸子百家基本上除法家外都给灭了。当然如果放到整个周秦之变的格局上考虑，那么主要还是儒家。墨家也批判，但是那套理想并没有定位在三代，墨子实际上更多是关于未来的说法。但是后来到了秦汉以后，统治者又逐渐经过漫长的过程，又把一些儒学的东西拿来用，但是到底后来那个东西在什么意义上是儒学，这本身就已经是一个问题。晚清有很多人说儒学在秦以后就不行了，他们都是希望复兴儒学的。他们恰恰认为2000年来都是强暴政治，他说孔孟如果想复兴，用我们现代的话来讲，那就是必须用自由民主那套把法家给颠覆了，它才有复兴的可能。实际上在晚清一直到戊戌之前很长一段时间，很多人都是这样的，他们一讲就说我们中国三代的时候是盛世，秦汉以后就堕落了，认为现在西方就是三代，他们儒学要比我们搞的好。因此那个时候并没有所谓的什么抵制西化，那时候没有这个观念。 <br />
<br />
但是我这里要讲的就是，我们讲他浴火重生也好，讲他重生，他就是死了，你要说它现在重新恢复，原来到底什么东西死掉了？现在我们又到底要恢复什么东西？我觉得这个有非常大的不确定性。 <br />
<br />
秦晖：我觉得我们每个时代的人，不管是从儒也好，反儒也好，实际上他都是对以前的东西进行一种解释。语言，尤其是一种语言大家都讲的时候，一百个人讲儒学可能有一百种讲法。汉以后大家都讲儒学，往往两个讲儒学的人之间的不同要远远超过非儒者。就像不同的基督徒之间的差异要远远大于基督徒与非基督徒之间的差异。所以我觉得我们现在讲儒到底有什么意义？这里我觉得我还是经常提到的，索绪尔谈到两个任意原则，就是语言只是一种符号，这个符号要表达什么所指呢？他认为这两者之间的关系是可以任意的，所谓任意原则，就是说一套话语你其实都可以任意解释的，一种符号可以任意给它填充什么东西，可是这样一来就有问题了，如果这样的话我们讲话还有什么意义呢？变成了没有任何确定意义的，完全就是语言游戏了。但是他说我们讲的话还是可以表达一定的意义的，我们用来表达意义的办法就是所谓的差异原则，你谈论某一个价值，你肯定心目中有一个针对性，也就是说你是针对什么而言。比如说我觉得不光是儒家，任何一种意识形态它基本上都是由一系列仁义道德也好，什么民主正义也好，等等，任何一种意识形态都是由一堆好词组成的，但是这个好词要表达什么意思呢？就是看它到底用这个指什么的。比如说仁义道德，仁义道德人人会讲，这个仁义道德是什么意思？那就取决于你所指的不仁不义不道不德的东西到底是什么？谈儒家，抽象的讲是没有什么意思的。而历代的人，凡是有激情的人弘扬儒学，他们实际上心目中也有一个针对的东西，在儒学发展的两千多年间我觉得是形形色色了，有的针对佛，道，有的针对杨墨等等等等，但是我觉得两千多年以来，如果要讲差异原则的话，人们谈儒学的时候老实说，最有影响的两个差异原则，一个就是针对中国的法家传统的，毛泽东后来曾经有过一句名言，说中国2000年来的历史就是所谓的儒法斗争的历史，他当然是站在法家立场上了。但是在法家的立场上谈儒法斗争，老实说核心就是认为儒家专制的不够，或者对专制构成妨碍。 <br />
<br />
秦晖：标准各异，关键是谁有这个权力。谁有什么权力说这个东西是好的，那个东西是不好的。所以我觉得在好与坏之间还有一个更高的原则。我觉得假如我们认为儒学是宗教的话，儒教和基督教，包括伊斯兰教，也包括他讲的墨子，等等。墨子有人说它也是一种宗教，因为墨家的宗教色彩是很浓的。我觉得这些都是可以共存的，而且也可以互补，惟一不能共存的就是异端审判和宗教宽容。所以我们现在要提倡的就是宗教宽容，思想自由，我们不能容忍的就是不管打什么旗号的，哪怕你打儒的旗号也好，你打西学的旗号也好，打法家或者别的旗号也好，那种禁锢思想的东西我们都要把它灭掉。 <br />
<br />
至于在理解任何一种学问，包括你讲的儒学，也包括墨子或者是西学，人们会觉得有些东西是好的，有些东西是坏的，而且这个东西老实说，社会上人们的评价也许不会一致。比如说你认为这个好，那个不好，别人也许不那么认为。我觉得这个东西很简单，在现代社会中允许个人自由的这一部分，那就是应该让大家个人自由。但是如果有一些最基本的人际交往的一些基本规则，需要有一些立法，那么这就是属于公共领域的了，这个东西当然就需要有民主原则来奠定，这两个原则基础上我觉得对每一种学问的多元化的理解，我觉得都没有坏处。 <br />
<br />
秦晖：我觉得这个问题用不着那么复杂，十博士他们自己不愿意过圣诞节，我完全支持，他们如果仅仅是一般意义上的提倡不过圣诞节，我也并不反对，大家他们如果要求国家出面，用权利剥夺一些人过圣诞节的权利，我觉得那是非常可怕的。 <br />
<br />
秦晖：他吸引你入教，这就是很糟糕的，需要抵制的。现在西方圣诞节已经世俗化的非常厉害，很多人过圣诞节就是乐一乐而已。他就算是教徒过节，我觉得也没有什么值得大惊小怪的，你传你的教，我传我的教，只要他不用那种超经济手段，或者是带有政权意味的手段，我觉得这也是一种交流，我倒是提倡我们这些弘扬儒学的人，他们应该跑到全世界去传教，像杜维明先生就是满世界传教，我觉得这样很好。 <br />
<br />
秦晖：这里首先要强调一点，我们怎么定义文化，这个问题在五四的时候就有很尖锐的说法，有人说孝是中国文化的特征。于是很多人说陈独秀鼓吹个性解放的，他们就是在破坏中国文化。说他们鼓吹万恶孝为首，百善淫为先。陈独秀当时有一个很有意思的反驳，我觉得很精彩。他说我完全赞成子女孝敬父母，我完全赞成媳妇孝敬公婆，我唯一不赞成的就是父母公婆借孝的名义来剥夺子女的自由，实行一种父要子死，子不得不死的一种很严酷的父权。这里面提出一个很重要的概念，就是中国人真有孝文化吗？假如真有，为什么要在那么严酷的父权下来实现呢？如果孝敬父亲长辈是非常自然的选择，自然而然的感情，没有这种严酷的父权，又怎么会破坏这种文化呢？所以当我们谈论文化、主流这些东西的时候，我们一定要区分这两者，我们到底是在提倡某一种宗教呢，我这里把文化和宗教几乎划等号了，也可以说在某种意义上来讲广义的宗教，尤其是符号化的宗教，你也可以说它等于是文化。我们到底是在提倡某一种文化呢，还是在提倡某一种制度。这个所谓制度就是我刚才讲的一种宗教审判的制度，政教合一的制度，还是一种宗教宽容信仰自由的制度呢？我觉得我们中国现在面临最大的问题其实不是到底是弘扬儒家，还是弘扬西学，还是弘扬墨家，其实根本就不是这个问题。 <br />
<br />
秦晖：还是我刚才讲的，还是到底我们坚持信仰自由，宗教宽容原则，还是坚持异端审判，政教合一原则。包括在文化问题上，我觉得中国面临的恐怕还是这个问题。至于刚才陈明提到的文化冲突，我觉得西方国家在世界上现在的所作所为，称赞他的人，认为他们是正在推行自由民主，谴责他的人，说他是在追求西方人，或者西方国家的那种世俗利益。比如抢石油，抢资源，垄断市场。我觉得这两种说法都非常有道理。但是如果说西方现在在对外政策，在全世界推广基督教，我并不怀疑西方的某些政客会有宗教偏见，包括布什显然就是这样，包括十字军那句话就是这样。但是他一讲这个话马上就捅了马蜂窝，他就不得不一次又一次道歉，而且他这个道歉是针对美国人的道歉，是向美国人道歉，不是向其他国家道歉，也就是说他的那套东西本身在美国人们就不能接受。如果亨廷顿真的认为国际关系就是所谓儒教与基督教，或者伊斯兰教的冲突，那么我觉得合乎逻辑的第一个主张，他应该在美国就恢复宗教法，把其他的宗教都灭绝了。否则就在纽约，就在华盛顿的这些异教徒不比在世界其他地方的异教徒更加危险吗？他们不是第五纵队吗？他们不会把基督教文明纯洁性给玷污了吗？如果他认为美国人本身是有宗教自由的，他可以不信基督教，他可以信别的宗教，但是他要出兵，在全世界推行一种美国人都可以不信的那个教，这在逻辑上怎么能成立呢？这种行为怎么才能行得通呢？我觉得这在逻辑上根本是不能成立的。所以我觉得美国人在外交上的那种行为，称赞他们的人说他们有点侠义精神，为别人的自由民主去打抱不平，你也可以说他们就是商人，就是唯利是图，或者甚至可以说他们是强盗，他们想抢某种东西。这两种说法我觉得都比说他们的外交是一种传教士的外交，恐怕要更近事实的真相。 <br />
<br />
秦晖：我觉得老实说，做节目我们不是做广告，也不是要达到什么功利性的效果，我觉得这个其实很简单，就是有这么一个机会大家来聊天，至于各位包括镜头前面的，包括网络前面的那些朋友，他们从我们这个讨论中如果能得到很多收获，这当然很好，但是这个收获本身也是见仁见智的。 <br />
<br />
秦晖：我觉得主要是讲我想讲的东西，我不是希望讲的是别人想听的，不管这个别人是指领导还是指一般的人，这不是在制定公共政策。我只是讲我的学术观点，我凭什么要考虑别人，这个别人如果是领袖，他一皱眉头我就吓的不敢讲了？ <br />
<br />
秦晖：只要领工资的就是御用学者，这个话本身就有很浓的审判的味道了，恐怕不至于。我觉得这个事情本来就是一个作学问的，不管是论坛还是个人，其实都是越多越好，多多益善。中国现在真正的问题不是什么公立学校办的太多，而是公立学校的学术自由空间太少，但是中国未来的思想前景，我觉得实际上是随着整个社会的自由度的提高，实际上从各个方面资源还是不少的。</div>

							
							
							
							
