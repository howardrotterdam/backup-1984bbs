一<br />
或曰：“东海一再强调要尊重民意，要以善服人，可见很懂得民意和服人的重要性。可你自己却常常不懂得尊重他人、尊重民意，不能让人心悦诚服，表现得很没政治水平，这不是限制了自己的发展么？儒家文化，见仁见智，自由派普遍反感，完全可以等到以后再谈。”<br />
<br />
这段话误解多多，问题多多，主要是混淆了政治人和文化人的区别。<br />
<br />
政治人求同存异，对于不同意见、思想和观点，可以“存”而不辩。文化人则不妨求同辩异。辩异乃大文化人的本份。释尊不能不破九十六外道，孟子不能不批杨墨诸异端。所谓不破不立，大破大立，破为了立。这与尊不尊重风马牛不相及。<br />
<br />
尊重他人不是苟同异端外道，尊重民意不是民意至上民粹主义。儒家对世人对民意当然要尊重，但更要依理、依仁本主义进行教化和引导。<br />
<br />
《调整两千年，一飞九万里》中所说要以善服人，是针对“在位者”说的。“在位者”最高境界是以善养人，以善服人属次优选择。别人服不服，民众服不服，政治人有必要关注，文化人则不必在乎。真理良知的普及弘扬受时代、地域、民智的局限，还要受传播渠道及各种人为因素的制约，有一定的渐进性和阶段性，不是一蹴可就的。<br />
<br />
顾名思义，文化人重在表现思想、文化、学术水平而不是政治水平。如果说政治人更尊重民意、注重团结的话，文化人则更尊重真理、注重明理，为此不妨“虽千万人吾往矣”。如果说政治人必须有团队合作精神，文化人则更应学习熊师十力“孤冷”风范和“孤往精神”。<br />
<br />
二<br />
熊师《十力语要》中说：“人谓我孤冷。吾以为人不孤冷到极度，不堪与世谐和<br />
。”强调“凡有志于根本学术者，当有孤往精神”他又说：<br />
<br />
“中国学人有一至不良的习惯，对于学术，根本没有抉择一己所愿学的东西。因之，於其所学，无有甘受世间冷落寂寞而沛然自足不顾天不顾地而埋头苦干的精神於中的生趣。如此，而欲其学术有所创辟，此比孟子所谓缘木求鱼及挟泰山以超北海之类，殆尤难之又难。吾国学人，总好追逐风气，一时之所尚，则群起而趋其途，如海上逐臭之夫，莫名所以。曾无一刹那，风气或变，而逐臭者复如故。此等逐臭之习，有两大病。一、各人无牢固与永久不改之业，遇事无从深入，徒养成浮动性。二、大家共趋于世所矜尚之一途，则其余千途万途，一切废弃，无人过问。此二大病，都是中国学人死症…。逐臭者，趋时尚，苟图媚世，何堪恬淡。随众势流转，侥幸时名，何堪寂寞。逐臭之心，飘如飞逢，何能专一。自无抉择之习，唯与俗推移。无所自持，何能恒久。故一国之学子，逐臭习深者，其国无学，其民族衰亡徵象已著也。而中国人喜逐臭，而不肯竭其才以实事求是，喜逐臭，而不肯竭其才以分途并进，喜逐臭，而不肯竭其才以人弃我取，此甚可忧”。<br />
<br />
三<br />
熊师此话，不啻空谷足音，金玉良言，也是对本文开头质疑者的最好回答。文化人只管“竭其才以实事求是”，只管“不顾天不顾地而埋头苦干”去，只问理真不真，不管人服不服。愚人浑人小人恶人逐臭之夫不服，正是道大理真的反面证明也。<br />
<br />
还复须知，对真理的尊重源于人人皆具的良知本性。就人性言，良知难致，毕竟有人能致，所以理真必有人服；从长远看，本性难明，终究会普遍大明，所以理真必能服人。一时一地的成败利钝，何足挂怀。<br />
<br />
自由派普遍反感儒家文化，我当然知道，十几二十年前就知道。以前以为他们的反感是由于不了解，只要有人出来把道理、把道德的深意和儒家的真面目讲清楚，会让他们改观的。谁知大谬不然，令我诧异又怜悯。<br />
<br />
本来也想存异求同、等求到民主自由以后再谈文化。但近几年来我终于明白：文化与制度，必须双管齐下、同步并进。没有文化之根道灾本，就缺乏自由追求的力量和制度优化的土壤。在中国，没有儒家的复兴和升级，就永远等不来、求不来这个“以后”！2009-8-14东海老人</div>

							
							
							
							
