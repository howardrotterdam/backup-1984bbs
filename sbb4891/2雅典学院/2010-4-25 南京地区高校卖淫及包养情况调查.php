&nbsp;&nbsp;我调查“校鸡”“小三有两年时间了，在这近两年调查的时间内，我尽可能去进入她们的世界，了解她们所想，所知，所求，最重要的是，我希望知道她们是因为什么原因踏入这个肮脏的圈子。什么外力这么强大，将这么多有些我认为原本比较单纯的女孩拉入无法拖出的泥潭中。<br />
<br />
&nbsp;&nbsp;在这两年中，我经常有冲动把一些女孩拉入自己的怀抱，用自己的身躯和意志保护她们，保护她们千疮百孔的灵魂，可我总会冷静下来“谁也拯救不了她们”很大程度的堕落是因为她们自己，我只能作为一个局外人，让别人知道她们的遭遇，决不能把自己拉下水。<br />
