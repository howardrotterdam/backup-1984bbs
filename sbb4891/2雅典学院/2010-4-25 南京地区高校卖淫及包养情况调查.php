&nbsp;&nbsp;为了保护隐私，在这篇调查中所有人物都使用化名，所出现的场所学校尽量不出现全称，由读者自己想象，调查里出现的人物都已经得到对方同意。谢谢成都网友无邪气，他的帖子给我以勇气，谢谢小史，凤凤等，你们可以让更多人了解社会中不为人所知却急需阳光的死角。<br />
