&nbsp;&nbsp;第三，希望大家认真的态度关注调查中的所提到的这类女孩，她们也许离你们并比遥远，有些甚至就在你们身边，和你们一起上课，有时候，你们的冷漠，会造就她们更大的悲剧。<br />
