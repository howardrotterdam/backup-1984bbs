<br />
&nbsp;&nbsp;如果说开了房没有那心思是不现实，何况小史是那种一看就能勾起男人欲望的女生，只是我一想到他是做那种职业的我就犯恶心，并且，做鸡的一般身上都带病，出于健康考虑我也不能越界。<br />
<br />
&nbsp;&nbsp;我说我们先聊会吧。<br />
<br />
&nbsp;&nbsp;她问我是不是第一次，我不置可否的耸耸肩。<br />
<br />
&nbsp;&nbsp;她笑笑，先去洗澡了，我叫她放心，一晚的钱我不会少她的。<br />
