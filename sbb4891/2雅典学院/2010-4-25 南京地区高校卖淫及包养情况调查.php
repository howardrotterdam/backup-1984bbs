&nbsp;&nbsp;我是小蛮，一名普通的在校大学生。<br />
<br />
&nbsp;&nbsp;我做这个调查将近两年了，一直没有勇气发表出来，前一段时间成都猫扑网友做了一份相似的调查，这鼓励了我，有些话，如果现在还没有勇气说，那么事实的真相就会被永远的歪曲，我觉得我有责任将这个调查公之于众。<br />
