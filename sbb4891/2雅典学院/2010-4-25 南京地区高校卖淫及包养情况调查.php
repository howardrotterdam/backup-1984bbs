“原来只是想攒点学费生活费而已”<br />
<br />
&nbsp;&nbsp;我第一接触“校鸡”是在大二上学期，我在社团活动的一位“朋友”问我需要“打炮”，他认识南京某所师范学校的校花，兼职做“校鸡”，人长得漂亮还清纯，还开玩笑说如果我是“第一次”他就请客。<br />
<br />
&nbsp;&nbsp;当时“校鸡”对我来说还算是非常神秘的领域，当时我还并知道我的那位“朋友”是皮条客，专门在我们学校发展“业务”，我只当是开玩笑，对他说“今晚我们去尧化门还是迈皋桥？”――这是我当时认为南京有名的红灯区。<br />
