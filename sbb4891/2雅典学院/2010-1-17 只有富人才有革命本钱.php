首先，高关税政策恰恰是在选举权极其有限的时代被提出，又在选举权扩大了若干次之后，才被放弃的。 那些极少数人为什么要提出高关税政策？是为了保护他们的消极自由不被侵犯吗？错&nbsp;&nbsp;显然唯一的目的是增加自己的财富罢了<br />
<br />
其次，新涌现的成功人士（其实就是新涌现的资产阶级）总是在争取自己的选举权。 他们为什么不肯乖乖地把自己的自由交到如此神圣以至于无法被侵犯的宪法，和拱卫着如此神圣以至于无法被侵犯的宪法的几个互相制衡的力量（咦？那是什么东东？以夷制夷么？）的手中呢？<br />
<br />
最后，美国甘蔗种植商和玉米加工商（注意，人家是富豪和大企业）游说提高了糖关税，而可口和百事可没跟他们打政治仗，而只是自己去找便宜的替代原料（提醒一下：并没发生“替代原料把关税保护的高价原料打得满地找牙”这种好事儿，其它需要糖的企业还是得买单的，最后会轮到消费者）――――喜欢关税的可不光是无知无识的工会，而受害企业也没兴趣维护自由，他们只做成本核算，而且很容易被各个击破――――谁掌握权力，谁就用它谋利，可不光是穷人欺负富人，a行业欺负b行业、企业欺负消费者，也是一样<br />
<br />
<br />
可见，问题并不是“宪政vs民主”，而是“有产者民主vs全民民主”罢了。<br />
当然，你可以说“我把零关税写进宪法”，这可能很好，但这样一来，我也可以把税率上限、对累进税的限制等写进宪法啊<br />
