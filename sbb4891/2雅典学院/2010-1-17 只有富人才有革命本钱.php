================================<br />
<br />
以上为转帖，不是扯淡就是调侃。我添一句如下：<br />
<br />
A：这鸡蛋真难吃。<br />
B：比没鸡蛋吃好吧？！<br />
<br />
我添这一句不是多余的，而是有意义的。比如秦晖提出“低人权优势”概念，他的意思是，你别牛逼，你的发展还不是建立在低人权这个优势上的吗？有了我这一句，就可以构成对秦晖的有效反驳――难吃有吃总比没吃的好。但还会有人说，在高速发展了30年的今天，还这么低人权就说不过去了。我说，中国的工业化城市化还远远没有完成，还有几亿农民要吃饭要工作要转变身份（不少城市人口同样面临类似问题），高人权了――高工资高福利，肯定就是高价格，而高价产品国外没竞争力，卖不动就要减产停产，就要减薪减福利，甚至辞工。如此，吃饭工作问题反而不好（甚至不能）解决了。所以，低人权是一种很现实又很无奈的态度和办法。<br />
<br />
当然，彻底解决的办法不是没有，比如彻底私有化，这样可以给人创造很多自食其力的机会。尤其土地私有化，好处是大大滴，比如一部分（可能会很多）农民工就会回到乡村，城市压力就会大大减少（农民回乡会减少城市住房需求，房价就会降），土地的污染撂荒也会从根本上得到解决（没人会污染撂荒自己的土地），“站在自己的土地上，种出自己的食物，他就可以傲视地主和商人，同时也可以与政治家抗衡。”（《美国的历程》中对类似圈地运动的西部开发情景的描述）等等。<br />
<br />
然而，中共不会这么干，因为：1，公有是它的命根子，私有了，它就一定丧失执政基础；2，中共的包袱太重，一旦下台，就不仅是丧失特权及由此带来的利益的问题，而是身家性命能否保住的问题。<br />
<br />
那怎么办？革命不可能。尽管军队对外战斗力肯定很差（人才逆淘汰军队比地方更甚决定了它不可能有牛逼将帅，装备训练经费的贪污浪费也比地方厉害决定了其战斗力不会达到应有水平），但对付国内肉身还是绰绰有余且经验丰富的。革命也不好。过程难把握（谁敢保证一定能控制暴民？），结果难预料（长期的今不如昔也不能排除）。革命违背历史良性发展的逻辑。好的制度都是演进而来的。英国革命就不说了，即使美国革命，也包含着诸多对传统的继承而非破坏和斩断，比如自治。北美的地理位置（还有英国与法国西班牙长期交战）决定了宗主国无法紧密控制它，所以，北美人有长达二三百年的自治历史，以至他们对英国的热爱之情居然是这样表述的：“英国的可爱之处还在于，他对殖民地人民撒手不管。”（大意，见《美国的历程》）。而中国的近现代传统都是取法法国和俄国的彻底暴力革命――革了政权的命、也革了社会的和文化的命。这样革命的结果还不如古代朝际更替的结果。<br />
<br />
革命不可能也不好，那怎么办？只有让中共自己去“折腾”。客观地说，30年改革开放中共已经放了一些权，也多少接受了一些真理（比如市场），放弃了一些谬误（比如阶级斗争），还确实取得了一些成果（起码解决了吃饭问题，比饿死几千万不知好多少倍，其实远不止这些）。问题是，中共会不会继续做到大致上的正确？我认为会，起码会有朝这个方向努力的意象。所谓“大致上的正确”，经济上就是，发挥市场基础性作用不动摇，而这必然要求其不再国进民退，尽管现在国进民退有加剧迹象，但我相信中共会适时刹车（假如国进民退是个真命题），因为全退回去等于中共自寻死路。政治上，你哪怕一党立宪也好，反正要想办法管住自己官员的权力不致滥用得太厉害（监督再严密的民主宪政国家也不可能杜绝贪污腐败，这也是经济学家主张小政府大市场大社会的原因，所以我说“世界是大致的”）。而从目前中共推行的问责制来看，你不能否认他有真心约束自己的想法和行动。<br />
<br />
当然，立刻就会有人提出：既然要限权（包含对权力滥用的制裁），没有反对党怎能保证他的左手会打疼自己的右手？何不一步到位实行真正意义上的宪政？说老实话，我这几十年就是这么盼的，只不过侧重点在经济瓦解上。可是我们要面对现实。现实是，让权就意味着掉脑袋，而反对党的出现就意味着让权的可能。所以，用反对党去限权是不可能的。这不是以我们的意志为转移的。此外，虽说宪政（多党的）有独立于民主而实行的可能，但中国人对民主的迷信决定了他们要求宪政的同时也一定要民主。这就更复杂和麻烦了。民主本身有其不可克服的致命弊端（这里略去不谈，要谈我可以谈很多，比方绍伟还深入浅出、通俗易懂），关键是，现阶段的中国根本无法民主、不能民主。1，民主更需要反对党，而中国没有可以担当起如此大规模转型的繁重历史责任的反对党。2，一民主就要选举，而一选举，暴民党上台的可能就很大。国家交给暴民就如同49年。3，现在民粹气氛太浓，即使暴民党不上台，弄出个查韦斯（软暴党），中国也将走上社会主义的老路。4，也是最关键的一点，一旦多党民主（包含宪政），社会就将面临彻底改造（这是国人对中共全面否定的必然结果），而这样的改造有违社会良性发展的逻辑――渐进演化。<br />
<br />
因此，只能交给中共自己去渐进演化。其间，民官的损益无论怎样此消彼长（实际在减少），都是历史发展中不可避免的代价，只能承认、接受和忍受。不承认、不接受和不忍受以至推倒重来，其代价只会大得多，而不会小得多。至于渐进演化的结果，也是非常关键的，我们还得做点心理准备。我的社会发展观是（若干年前不是这样，那时我相信突变），不同民族、不同国家有其不同文化生活传统，这决定了他们的发展轨迹也不同（历史已经证明），其发展方向、节奏和结果也不可能相同。东西德、南北韩等是偶有的例外，就是日本与欧美以及欧美国家之间也差别甚大。所谓“大同世界”完全是骗人的和不可能的。中国几千年来本身就是一个专制-奴才二分的国家，上面想的是如何加强管制，下面盼的是主子如何贤明。有了民主这个洋货后，专制和奴才的二分也没改变多少。民众不忘寻找能代表他们的人，掌权者则专门热衷代表他人。一个希望被代表，一个想强制代表。于是，民主成了奴才和专制共穿的外衣。所以，尽管我崇尚自由、市场（或许还有宪政民主）的心没变，但我感觉这些目标在中国短期内（至少三十年吧）比较难实现，甚至我的玄孙能否享受到也未可知。这或许就是中国的自发秩序使然吧。<br />
<br />
可对于相信自发秩序的人来说，是不是还有值得一说抑或探索乃至期待的东西，即，中共目前倡导的回归传统文化。对此，一种怀疑是，此举不过是权宜之计，一方面可以借其抵挡西化，一方面巧妙掩盖中共可能的对马教的实质性放弃。除此之外还有其他更深的原因吗？比如，中共或许早在暗中进行过彻底反思――当年疾风暴雨式革命（包括辛亥、五四新文化、共产及文革）都是错的，断送了由戊戌变法揭示的对传统文化的尊重也可能使中国走上英国道路的前程。倘若真有如此考量，那我就不得不对中共刮目相看了。何况，正如现在已有越来越多的人所认识到的那样，坚持中国传统文化的确有着能够走出一条完全不同于西方的繁荣自由之路的可能。当然，这些都只是猜测，暂时存疑吧。<br />
<br />
接着说“折腾”。不革命，还要交给中共去“折腾”，这并不代表民众可以闲着，也不意味着民众可以任由中共宰割。换句话说，（如果自发秩序不那么起作用的话）中共可以建构，民众也可以建构。过去多年我们的呼吁已经取得不少成就（收容遣送恶法的废除、邓玉娇案的峰回路转、周老虎的真相大白、拆迁条例的即将修改等等），这些无不是在具体个案上呼吁的结果。而当我们空喊民主平等自由人权这些口号并把它们串连起来一并要求时，多少就会给人连根拔起、全面改造的印象。所以，我们不妨继续在点上求进，甚至可以集中在一点：争取财产权。<br />
<br />
争取财产权？这不是很俗吗？没错。但，多数人都是俗的（执意装逼的也不例外）。即使宗教方面，天主教不改俗一点的话，也不会有那么多基督徒。财产权听起来是俗，其实它与保障人权很有关系。比如，前年FDII（意思是国民可以境外投资了）推出后我写了一篇短文，要求当局给我们提供知悉更多外国信息的渠道。理由是，FDII是你官方同意开通的，我买了外国资本品，我就有权知道外国信息，你不主动履行为我们提供外国信息的责任和义务，那你就无权阻止我“翻墙”了。因为影响资本品价格的不仅是经济方面的信息，还有政治的军事的科学的等等，所以，说你已经为我提供了经济信息是不够的。你看，仅仅一条资本流通渠道的开通，就有可能使我们享受到新闻自由。而我并没有直接争取新闻自由权，却可以借助保护财产权的力量迫使当局无法拒绝我获知外国信息的要求。而且对财产这种切身利益的追求力也是最容易被召唤的。正如马基雅维利所说：“人们忘记丧失父亲的痛苦比忘记丧失遗产的痛苦还要来得快一些。”<br />
<br />
财产权还有很丰富的内涵。当我们把对言论自由的侵犯理解为对财产权的侵犯时，也就容易反驳对言论自由的限制了。比如，在拥挤的剧院大喊“着火啦”这件事，它并不是侵犯了所谓“公共品”，而是侵犯了他人花钱买来的安静欣赏这一环境的财产权。认同了这一点，我们就可以反过来说，假如一个人的言论没有侵犯到他人的财产权时，那么对这种言论的限制就是多余的，不合情理的，或者是非法的（从法是本身就存在的、是“被发现”出来的这个角度）。如此，那个宪章侵犯了谁的财产权了吗？没有，因此对它的打压是错误的（当然我不赞同另有原因，它是或者起码会引起全面改造）。<br />
<br />
把诸多虚幻的权利归结为财产权这个比较实的东西，可以解决很多问题。再比如，人的身体本身就是他的财产，那么，对身体的伤害就是侵犯了他的财产权。以此还可以推出对计划生育的批判。生孩子本身就是人的扩大再生产，这种生育（产）权就是人身这个财产本来就该追求的，甚至可以这么说，产权的第一要义就是生育权。那么，限制生育就是侵犯了人的产权。产权还有很多说头，比如与《大宪章》一样古老的“无代表不纳税”。最有可能喊出这句话并使其伴随了英国议会下院的诞生呢？有财产权的人（已有另文说明，这里不再赘述）。英国议会下院议员至今多数仍为社会上层人士担任并不是一两次偶然选举的结果。至此，我可以转述罗斯巴德的一句话了：“唯一的人权就是产权。”<br />
<br />
所以，各位，不要小看财产权。有了财产权，即使你想革命也比穷光蛋容易得多。因为革命是很要花钱的，正如托克维尔说：“只有那些一无所有的人，才丧失了他们曾经有的反抗”。所以我说“只有富人才有革命本钱”是没错的。当然，当你富起来的时候，你也不想革命了。那么，如何富起来呢？靠市场。而市场只能建立在私有产权和自由竞争的基础上。所以，呼吁私有产权和自由竞争吧，以此逼迫中共继续向市场妥协。<br />
<br />
