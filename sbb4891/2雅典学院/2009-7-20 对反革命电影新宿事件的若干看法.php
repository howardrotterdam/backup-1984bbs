													<h2>对反革命电影新宿事件的若干看法</h2>
						
						
																											<div id="postmessage_169652" class="t_msgfont">标题：《迷失东京之新宿事件》<br />
<br />
副标题：《主旋律电影，成龙电影和对新宿事件的简短评论包括对广电总衙门禁掉此片的看法》<br />
<br />
 <br />
<br />
对于成龙的电影，我一直存在一种强烈的排斥感，原因是其中总是充满大量的意淫段落。这与国内某头文字八的国营电影厂拍摄的那些电影所表达的主题在本质上是一样的，简而言之就是中国最牛逼，与之作对的都是傻逼，最终的下场是我们代表的正义战胜了邪恶，邪恶势力死无葬身之地。如此这般的叙事逻辑，我将其命名为奥特曼逻辑，也叫做意淫。意淫一回两回还好，次数多了难免伤身体，让人感觉很痛苦，甚至出现干呕的恶劣症状。<br />
<br />
在说《新宿事件》之前，请允许我跑一下题，先讲讲我对国产主旋律影视作品的一些浅薄的认识。<br />
<br />
我国拍主旋律影视作品并没有错，一个国家需要用各种形式和方式来凝聚人心。但我国的主旋律影视作品的问题在于，它没有半点我们中国人经常自我标榜的含蓄内敛作风，就是赤裸裸的告诉你，这是一部主旋律电影或者电视剧，目的就是让你狂热的充满民族主义的爱国，不仅如此，广电总衙门以及中宣部等机构还规定每年要进行“量产”，他们以为这是在种粮食。我觉得这不叫爱国主义教育，这是爱国主义说教，时间久了我们就会麻木掉，甚至物极必反适得其反三反五反・・・<br />
<br />
我认为主旋律电影要尽量客观，正所谓酒香不怕巷子深，如果你真是那么牛逼就不怕真实历史的考验，如果是杜撰历史，目的就是为了教育和清洗人民大众的脑子，这就是一种很邪恶的愚民做法。<br />
<br />
比如拍摄一部抗日战争题材的电视剧，我们不能够将日军搞的像傻逼一样，更不能图省钱找一帮中国人来扮演日本人，甚至还让“日本人”说一口流利的普通话。这样的实例并不难找，你只要打开电视随便找一部此类型的电视剧即可。这其实就是典型的意淫。一群中国群众演员扮演八路军，同另一群中国群众演员扮演的日军进行一场意淫表演，和小孩子过家家一样可笑。<br />
<br />
了解历史的同学都知道，抗战打了八年。如果日军真是如我们影视剧中所展现的那般，那对比之下，我军岂不更加傻逼。这就叫自己打自己的脸，在我眼里，我国的主旋律电影就一直自我陶醉的打自己的脸，而且还乐此不疲。相关部门如果能如我这般心思缜密的忧国忧民，也就不会搞诸如五个一工程这种毫无意义的自我歌颂的东西了。只有充满真诚和人文关怀的影视作品才能真正的触动人的心灵，直指人的灵魂。<br />
<br />
 <br />
<br />
成龙的电影给我的感觉就和那些粗制滥造的国产影视剧差不太多，虽然形式上精致了一点，但本质上却是一样的。成龙代表中国人形象，与外国人PK，最终中国人成龙了，外国人成傻逼了。别管外国人用人海战术还是高科技武器，我们的民族英雄成龙总会战胜他们并且保证自己不死。<br />
<br />
也许是这样的电影拍多了，连成龙自己都烦了，于是就拍了一部《新宿事件》。我个人感觉这部电影还是很好看的，比成龙以往的电影有很大的不同。在这部电影中，成龙终于不用武艺高强上蹿下跳了，几乎变成了一个正常的人。电影的故事本身很好，戏剧化的讲述了一个社会现实，虽有少量意淫，但完全可以忽略不计。<br />
<br />
这部电影中的故事，一群中国非法移民在日本帝国主义的悲惨遭遇，很容易拍成一部“奥特曼逻辑”的电影：中国移民到了日本，经常受欺负，但大家很团结，虽然身份是民工但个个有一身不知从何而来的中国功夫，在大哥成龙的带领下一举扫平了东京新宿歌舞伎，最终成为了一带东京教父，所有黑帮臣服旗下，人们过上了幸福的生活，最后一个镜头是过新年，成龙穿着一身唐装舞狮子。<br />
<br />
但好在这个电影没有这样处理，上面只是我非常主旋律的意淫。电影中的真实情况是：经过短暂的安宁，成龙一伙开始因为个人利益而内讧，黑帮一开始就将成龙当作一颗棋子，警察也对其四面夹击，最终的结局是成龙和他的一众兄弟在异国他乡死于非命。这个结局是非常合情合理的，因为按照现实的情况，这样的结局其实从影片的一开始便就不难预料了。<br />
<br />
以上的这个对比，就说明了这部电影的成功，它没有按照常规的方式去处理那些情节，而是相对现实主义一些。在我国，不用绝对的现实主义，学新浪潮将摄影机扛到大街上偷拍。我们只需要稍稍的现实一点，电影就会很好看。<br />
<br />
电影中还有一些人物塑造以及剧情上的小瑕疵，比如成龙东北人形象演得很假以及徐静蕾为何突然成了黑帮老大的老婆等，但好在这些小问题对电影本身所传达的主题没有造成太大的影响。但我还不得不唠叨两句，徐静蕾的演技真是很不好，虽然学会了几句日语。搞不清楚香港那帮人为何总是找她演类似的角色，因为她博客点击率高吗。<br />
<br />
如果说徐静蕾角色的作用是成龙去东京这个行为的推动力，尚能说得过去，那么范冰冰就完完全全是个花瓶了，她的出现就是为成龙的失恋抚平伤口，并且在适合的情况下，被成龙甩掉，以此来反衬出成龙对徐静蕾的一网情深。如此这般，就构成了一段非常吉祥三宝的三角恋，延伸出来两段有些莫名其妙的感情。感情是必须要有的，但一段就可以了，这样还稍显主人公的落寞，最后的结局也更能动人心魄。<br />
<br />
全片的亮点在结尾处，人人下场凄惨，男主角们死光光了，女主角们也都成了寡妇，最后也算是个开放式结尾，没有具体交代成龙死掉。希望这不是在为拍续集所做的一个狡猾的准备。<br />
<br />
或许是广电总衙门感觉中国人在异国他乡没有如以前那般将外国人揍得像傻逼一样，所以气不顺给禁掉了。对于此就不说什么了。总衙门的思维方式总是那么诡异，动辄就是毛泽东在延安文艺座谈会上的讲话。好在现在还有网络，不至于让我们活的那么无趣无聊和无知。<br />
<br />
最后我想说的是，看完这部电影，我感觉还是我们比较好一些，像日本那样民主社会走的太极端，社团合法化，搞的社会乱七八糟的也不好。从这一点来讲，广电总衙门禁掉这部电影是不对的，让我们失去了一次批判资本主义腐朽思想的一次机会。<br />
