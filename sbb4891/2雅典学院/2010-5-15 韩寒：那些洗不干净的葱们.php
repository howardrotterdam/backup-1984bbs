													<h2>韩寒：那些洗不干净的葱们</h2>
						
						
																											<div id="postmessage_616404" class="t_msgfont">最近，福建有了高教十条。其中最让人瞩目的是第二条――在教育教学工作中散布违反党的路线方针政策、党的基本理论、国家法律法规等错误言论，对学生确立正确理想信念和政治信仰造成不良影响的，实行“一票否决”，违者将被解聘。<br />
<br />
<br />
<br />
让人宽慰的是，看到上文，我本以为要实行“一枪枪毙”，但这仅仅是“一票否决”，比毛泽东那会儿进步多了。至于一票什么样的人能投出这一票，我并不关心，我关心的是这个方针政策和基本理论实在很难把握，当权者在要求我们统一思想的时候，自己经常统一不了思想，在我幼小的记忆中，我隐约记得我的高中课本里讲到三权分立，政治课本和政治老师都说，三权分立是个好东西，但最近我一直看到官方的文章和讲话，说三权分立是错误思想。你知道我是一个只有高中文凭的人，我的政治课学到这里就退学了，我只觉得很困惑，我为那些散布过这个错误思想的政治老师和教材编委的命运感到担忧。他们一直在念着领导给的稿子，弄不好还要还给领导办了，原因是那稿子是领导昨天晚上的想法，今天领导起床以后想法变了。<br />
<br />
<br />
<br />
对于这种感受，我看到过一个精确的评价，大意是……他上了车，马上打开右转向灯，往前开了一米，他左转了，左转也就算了，没想到他居然调了一个头。<br />
<br />
所以，被这样的司机撞死只能自认倒霉。<br />
<br />
<br />
<br />
同时，我关注到今天的一个新闻，袁腾飞老师和天上人间夜总会在同一天被查办了。袁腾飞是一个历史老师，教了学生一些正儿八经的历史。要教这个国家一百年以内真正的历史是有点危险的。这些历史有什么问题么？有人说有问题，犯下了严重的错误，我很希望两派人可以坐下来，一条一条的对证辩论，到底哪一条错在了哪，我们也好旁听旁听。如果说真有问题，我认为在我国现有的情况下，唯一的问题就是超前了，未来的历史书里迟早是这样写的。<br />
<br />
新闻记者追寻真相，历史老师讲述历史，作家文人写点真话，电影导演拍摄现实，轻则犯下了严重的错误，重则走向了犯罪的道路。但是，就算像洗菜一样洗脑，总有那么几颗葱是洗不干净的，以前，有人要把这些葱割干净，但是随着时代的变迁，那些人只要求这几根葱自顾自的长着就行，但是如果你试图告诉其他的葱，其实你也可以，他们马上会被装蒜的压死。<br />
<br />
<br />
<br />
很多人认为福建下的文是因为袁腾飞老师被批评查办以后的反应，我认为不是，在敏感院还没有表态的时候，政府哪有反应这么神速，各部门配合的这么好的时候。这只是一个巧合，是一个教育部门的常规规定。这一条，在各行各业中都有，说法不同而已。就好比所有的抽奖活动中都有一条，解释权都归活动举办方所有。而我不想去探讨什么谁有评判另外一个人的思想是正确和错误的权力等话题，这个话题没有意义，因为答案很明显，谁有这个权力？当然有权力的人有这个权力。一切有利于他的利益和权力的，当然就正确的，一切不利于他的利益和权力的，当然就是错误的，你只要掌握了这个规律，你就不用沉溺和揪心于什么错误和正确的判断了。<br />
<br />
<br />
<br />
至于大部分的历史老师，语文老师和政治老师们，你们觉得在未来的历史，语文和政治教材中，你们将会得到什么样的一个评价，扮演什么样的一个角色？也许你是一颗身不由己的植物，但是你的每一个学生都是你的种子。尝试着真正的做一个教师吧，教给你的学生常识和思考，独立与正义，为了年老时向你子孙提起你曾经担当的这个职业的时候心存骄傲，而不是满怀羞愧。<br />
<br />
