													<h2>韩寒回应全球影响力争议：我不是国际韩</h2>
						
						
																											<div id="postmessage_616463" class="t_msgfont">在美国《时代》周刊刚刚揭晓的“全球最具影响力人物”排行榜中，韩寒以将近100万的投票数获得了票选第二，并在最终“艺术家和娱乐界人士”的总排名中名列第24位，成为排名最高的中国文化人物，这次评选也再次将生性腼腆的韩寒推到大众面前。<br />
　　对于“全球影响力”的看法，韩寒有自己的解读，“中国真正有影响力的人(未必是好的影响力)基本都是在搜索引擎上搜不到的，主要是这帮人不滥用自己的权力影响力就行了。”<br />
<br />
　　他更认为，自己一点也不“国际”，就是一个乡下孩子。<br />
　　影响力<br />
“该不该杀韩寒”票数也会很高<br />
<br />
　　Famous：这次在《时代》周刊的评选上成为“艺术家与娱乐界人士”中唯一一位中国人，有没有人因此叫你“国际韩”？<br />
　　韩寒：说不上“国际”啊，我就是一个乡下孩子，之前我也说过自己是“郊区韩”，但“郊区”这个词可能好多人不是特别明白。其实我干的事和以前也没什么区别，就是每天吃饱了睡，然后起床，上上网，写写文章，现在我经常半夜写一些东西，白天起来一看，啊，原来是做梦的时候写的，然后就被删掉了嘛。<br />
<br />
　　Famous：但是你在票选中排名第二，仅此于穆萨维，这个影响力还是很大的。<br />
<br />
　　韩寒：说到影响力，不是我个人的影响力大，影响力大的人是在投票的那些人，要是什么时候都能以投票数来作为影响事情的标准，那可能世界就会变得美好一些。但也说不定，因为我们太穷了。如果要搞“是不是应该把韩寒杀了”的投票，没准也会有很高的投票数(笑)。<br />
<br />
　　Famous：你对穆萨维投票数第一有什么看法吗？<br />
<br />
　　韩寒：其实这个挺奇怪的，我俩都处在亚洲互联网有管制的国家，本身依靠选票决定的事情就比较少，说明这样地方的人对于投票的激情更大吧。<br />
<br />
　　Famous：你觉得LadyGaga算是有影响力吗？她在“艺术家和娱乐界人士”中排名第一。<br />
<br />
　　韩寒：她算是吧，大家都老说她。而且国内很多明星也都会模仿她。(大家同样也老说你)我是太特殊了，只能在中国这样的土壤里值得关注，别的国家都没有这样的环境。<strong>也有人问我，为什么我的书不在美国、日本出版，我说那些国家要出我的书干什么？我写的仅仅是常识。在人家看来，我写的是什么？是历史么？你也可以说，在中国有影响力就是对全世界有影响力，但我认为，事实上中国文化对世界的影响力还是小的。</strong><br />
<br />
　　Famous：在你心中，什么样的人才算是真正有影响力的？<br />
<br />
　　韩寒：文无第一，武无第二，很难准确形容出来。<br />
<br />
　　Famous：在最近的一次沙龙中，梁文道、陈丹青、吴思、张鸣等人，认为“中国大学教授的影响力加在一起，也比不上一个韩寒”，你认为他们说的有道理吗？<br />
<br />
　　韩寒：比如梁文道、陈丹青，我也很喜欢他们，很多人的很多东西也都在影响着我，会受到各种启发，只是我们可能没有互相影响的风气。现在的年轻人很难想象他们是在看书的，更多人是在网络上浏览简讯，或者玩游戏、聊天。现在的大学生和以前的也很大不同，都没有热血，光教授有热血也没有用，各种东西都在代替着信仰。其实我们连“影响力”到底是什么都还说不清。<br />
<br />
　　关注度<br />
<br />
　　因为大家都要找个出口<br />
<br />
　　Famous：我们换个轻松一点的话题吧，看到了前不久你拍摄的一组“裸照”，怎么会想到拍尺度这么大的照片？<br />
<br />
　　韩寒：其实那天的衣服是穿了很多，该穿的地方都穿着呢。因为那家是赛车媒体，如果是传统媒体我不会这样。赛车媒体很不容易，每场比赛他们都会去，他们算是很弱势的群体，中国那么大就这么一本做赛车比赛的杂志。我觉得我应该支持一下中国赛车事业。<br />
<br />
　　Famous：你好像基本不接电话，偶然回短信，我看你自己也写了关于入选《时代》的事只回复了《新京报》和《南方都市报》，接受媒体采访这一点你有什么标准吗？<br />
<br />
　　韩寒：就是自己比较喜欢的媒体，和有朋友在的媒体。其实一般来讲，我比较喜欢的媒体也都是有朋友在的。媒体是很难定位傻逼或者不傻逼的，因为即便有的媒体是，在那家媒体工作的记者也不会都是那样，(媒体)里面那些70、80后的从业者不会有太大的不同。但是有的媒体真的很好笑，有些论坛也想采访我，但他们的媒体又同时把我设置成屏蔽词，我说那你还来找我干吗呢？<br />
<br />
　　Famous：感觉好像就是在2009年中，你接连被很多媒体评为年度人物，“公共知识分子”的形象也被特别突出了，你认为2009年你到底做了些什么？<br />
<br />
　　韩寒：过了一年也就是地球绕太阳公转了一圈，这关我什么事？说到底我也只是一介书生，也不是什么知识分子，也不是什么文化精英，也不是什么各种各样的其他东西，在我脑子里根本没有公共知识分子的概念。我觉得是因为大家都需要找一个出口，所以我的关注度可能就高了。<br />
<br />
　　我对这个称呼(公共知识分子)完全不感兴趣，我从来就这样的。因为别的个体他们都不和我玩了，所以我就只能和这些公共事件玩了。<br />
<br />
　　上海<br />
<br />
　　我始终热爱，但是它没什么文化<br />
<br />
　　Famous：现在世博会是个很热的话题。<br />
<br />
　　韩寒：本来世博会并不是一个规模如此大的展会，随着信息的流通越来越便捷，世博会正在渐渐式微，是中国将世博会升格了。我出生在上海，我始终热爱这里，希望这个城市真正美好，虽然我的老家已经被严重的污染所占领。但是上海是一个没有什么文化的地方。别的大都市，你可以说，我们这里有什么建筑，什么酒店，什么大街，什么豪宅，这些上海市也可以自豪地宣布，我们这里也有；但当人家要说，我们这里有什么作家，什么导演，什么艺术家，什么艺术展，什么电影节，上海就没话说了。发展真正的文化就必须放开尺度，放开尺度必然百家争鸣，百家争鸣必然开启民智……<br />
<br />
　　Famous：你没有加入任何组织，好像外界并不能提供给你更多让你感兴趣的帮助。你对生活还会有什么需求吗？<br />
<br />
　　韩寒：我当然还会有需求。但有一些是太隐私的事，有些又不太好说。<br />
<br />
　　Famous：最近有什么让你感兴趣的文化作品吗？<br />
<br />
　　韩寒：我很想看话剧《宝岛一村》，听说不错。可惜上次演错过了。<br />
<br />
　　韩寒语录<br />
<br />
　　房子和女人<br />
<br />
　　女人要买房，不如休了她<br />
<br />
　　Famous：听说你自己的年薪刚好过百万，这个数字准确吗？<br />
<br />
　　韩寒：百万年薪在赛车比赛的时候差不多，现在办杂志还会往里贴一些。<br />
<br />
　　Famous：现在的房价你买得起房吗？<br />
<br />
　　韩寒：买不起，如果买的话要贷款，而且我觉得还是租房划算，因为现在的租金已经不够还贷款了。如果你碰巧有个非要缠着你买房不可的女人，那我觉得还是休了她好(笑)。<br />
<br />
　　Famous：但很多人都会觉得，只有自己买房了，才算在一个城市真正立足了。会不会因为你是上海人，所以对房子所带来的安全感要求没那么强？<br />
<br />
　　韩寒：在大家都没有信仰的时候，只能依靠房子带来安全感，而且房子确实也能带来安全感。我也不反对买房，只是觉得不划算。<br />
<br />
　　Famous：你从来没有遇到过要求你买房才能结婚的女人？<br />
<br />
　　韩寒：那当然，对于那样的女人，我只有两个字：滚蛋！<br />
<br />
　　淘宝店<br />
<br />
　　算为社会做贡献吧<br />
<br />
　　Famous：你的淘宝书店生意很好啊。<br />
<br />
　　韩寒：主要是东西都寄到了(笑)。其实开店挺累人的，还要受到很多非议。有人觉得我爸在借我赚钱，也有人觉得我拿读者赚钱。其实我一年也就卖一万多本书，开店的初衷完全是因为开始的时候我每周都要收到大量读者寄来的书，要求我签名，我总不能签完名就把书给黑了啊，还得再寄回去，这样长期以来，我就觉得不如开个店。书我都是以六折批发来的，原价卖，一本可以赚几块钱，一年大概可以赚到10万，但是我的店请了三个人，所以其实总的收入差不多是持平的，有时候还会亏。有人说我开店能赚很多钱，我觉得他们真的不会算账啊，每本书我都要签名，一年也得签几万字，哪有人赚钱这么辛苦？但这样也能解决几个劳动力，算是为社会做贡献了吧。<br />
<br />
　　代言<br />
<br />
　　总要给我留个活口吧<br />
<br />
　　Famous：现在你也会接商业代言吗？<br />
<br />
　　韩寒：我会接代言。一方面是因为要办杂志，商业合作也是避免不了的，还有就是很多时候我一年才写一本书，有时候都写不了一本，像我写的博客那些文字也都是免费的，大家总要给我留个活口吧！我赛车赚的钱基本都贴了杂志，杂志办了一年半还没有出来，审查永远有问题，我应该算是国内名气和年收入换算最不匹配的了。<br />
<br />
　　杂志<br />
<br />
　　底线内我比任何人都容易屈服<br />
<br />
　　Famous：你算是原则性很强的人吧，你会因为什么事情妥协吗？<br />
<br />
　　韩寒：我经常要妥协，比如我现在办杂志《独唱团》，刚刚又审了一次，有很多修改，不能提“羊肉”，“千禧年”也不能提，都会涉及到宗教问题。我有一个原则和底线，没有到这个底线，我比任何人都容易屈服。但如果触及到这个底线和原则的话，我是绝对不会屈服的。记得曾经有一次在北京和朋友新组了一个车队，要给车队做宣传。我不喜欢做什么宣传，但那次我去找了当时国内很差的一个汽车媒体，还得给人家塞500块红包。<br />
<br />
　　偷拍<br />
<br />
　　偷拍比给钱的报道真实<br />
<br />
　　Famous：前不久在纵贯线演唱会上，你和女朋友一起被拍了，给你带来困扰吗？<br />
<br />
　　韩寒：如果有小孩子还挺好的啊，我就不会是一个人在战斗了(笑)。其实不会有困扰，我在做更大的坏事的时候也不会让人知道(笑)。我那次的照片被拍出来之后，《南都周刊》的卓伟还给我发了个短信，求证那张照片。其实我挺喜欢偷拍事业的，这些才是真实的，像很多报道也都是假的，都是给钱才报道的，比起来我更喜欢真实的娱乐报道。</div>

							
							
							
							
