胡温正式分道扬镳<br />
<br />
胡锦涛与温家宝各走各路，「胡温新政」正式寿终正寝。名义上胡温在两年後中共「十八大」才会从政治局退下，但自今年年初，这两位中南海领军人物已貌合神离，尤其是在意识形态上分歧越发明显。记得胡温在○二年「十六大」上台後的八、九个月内，确曾尝试推出相对江泽民时代较宽松、「以人为本」的政策，但很快此「新政」因为不敢碰政改禁区而失色。现在胡温这两位同生於一九四二年，同在甘肃省起家，且曾并肩顽抗「上海帮」保守势力的战友分道扬镳，令人对中共这「永久执政党」进一步失去信心。<br />
<br />
温总文章不提胡锦涛<br />
<br />
中国刚取代日本成为全球第二大经济体，但大陆ＧＤＰ大跃进重量不重质，跳不出前总理朱熔基多次批评的「粗犷式增长模式」，加以房地产等领域泡沫严重，加剧了贫富阶层的矛盾。胡总虽然身兼中央财经领导小组组长，但他把经济问题通通归咎於老温的弱势领导；年初胡总要求温总把宏观调控大权交给第一副总理兼团派接班人李克强，但温断然拒绝并且凌厉反击！<br />
<br />
温总的官场自o反击战的重点是树立自己作为党内最忠诚改革派的形象，不但继承了邓小平与前总书记胡耀邦的遗风，而且有魄力在二十一世纪重燃政治改革的烈火！之所以今年四月十五日胡耀邦死忌当天，温在《人民日报》发表文章高度评价胡的改革功勋，顺带回忆当年自己陪同胡考察贵州，获得此改革巨人的言传身教。但温总的文章对时任贵州书记的胡锦涛却一字不提！<br />
<br />
八月下旬老温主持在深圳举行的特区成立三十周年庆典，向邓小平像深鞠躬後，温重提政改在整个改革进程的核心地位。温总强调「没有政治体制改革的保障，现代化建设的目标就不可能实现」。他似有所指的说，中共要「继续解放思想，大胆探索，不能停滞，更不能倒退」。这位自诩为改革总设计师门生的总理更重复老邓的名言，即不改革「只会死路一条」。<br />
<br />
新政是场美丽的误会<br />
<br />
明眼人都知道，温的矛头直指负责中共大政方针的胡总书记。胡总近年醉心於「马克思主义中国化、现代化与普及化」，与思想同样僵化的政治局委员李长春与刘云山在意识形态、文化出版等领域大开倒车，搞到知识界万马齐。胡天天喊「发扬党内民主」，实则出尽办法把共青团派的哥们提升到党政高位。遗憾的是，温总的指控虽然有理，但这位「中国影帝」除了在关键时刻出口术替自己的「改革家」形象烫金外，并没有对政治改革做过实质贡献！例如，温总有没有在政治局为异见人士或维权律师说过半句话？共和国总理有没有尽力防止家人利用特殊关系发大财？答案是没有！「胡温新政」只不过是场美丽的误会，到今天连胡与温都埋首於传统的政治斗争游戏，试问谁还会对中共的改革前景抱丝毫希望？<br />
