==========================================================================<br />
<br />
<br />
但天底下傻逼确实很多呀，就好比当年老百姓支持共产党绝对不是因为老百姓有什么共产主义信仰，而是因为国民党太他妈不像话了，1949里有类似情节吧？可咱能说中国的大多数老百姓都是有奶就是娘的傻逼么？<br />
<br />
那些上访的维权的，也未必懂什么民主自由的道理，如果党和政府真心解决他们的问题，照顾他们的利益，他们保证不会闹，说不定还会感谢政府一番，能因为这个说他们都是些随风倒的傻逼么？<br />
<br />
我不觉得这是价值观问题，换句话说，人们因为厌恶某些自由主义者的个人表现而失去支持自由民主行动的兴趣，未必是因为他们的价值观变了，或者说，那些支持民主自由人士的群众原来就不是因为什么“自由民主”或者“普世价值”的价值观而选择同情或者支持的。如果他们是出于自觉得价值观，那他们就不再是群众或者人民了，正如当年的老百姓，如果他们已经具有了共产主义价值观，他们就不再是群众，而是真正的共产党了。<br />
<br />
所以，我觉得，某些自由主义者以为别人由自己的恶劣表现而不再支持甚至反对自己，以为这是对方轻易改变价值观甚至得出对方是傻逼地结论 -- 这才是真正的傻逼，这不过是自欺欺人而已。<br />
<br />
找不到对象的人当然可以说别人都没品位，找不到工作的人当然也可以埋怨雇主眼拙，搞民主自由的大佬们要是也这样，恐怕上帝也看不到中国民主自由的那一天了。<br />
<br />
从这点上看，现在的柿油党比之当年的共产党，差的实在是太多了。<br />
<br />
哈耶克的书少看几本也不妨，先从实事求是作起吧。<br />
