													<h2>中华民国退出联合国告全国同胞书</h2>
						
						
																											<div id="postmessage_337131" class="t_msgfont">　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　原帖作者：白衣卿相<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;<br />
海内外全体同胞们：<br />
<br />
　　第二十六届联合国大会违反宪章规定，通过阿尔巴尼亚等附匪国家之提案，牵引毛CCP帮窃取中华民国在联合国及安全理事会中的席位；我们本汉贼不两立之立场及维护宪章之尊严，已在该案支付表决之前，宣布退出我国所参与缔造的联合国。同时声明，对于本届大会所通过此项违反宪章规定的非法决议，中华民国政府与全中国人民，决不承认其有任何效力。<br />
<br />
　　毛CCP帮是中华民国的一个叛乱集团，对内残害人民，罪恶如山，乃全中国人民尤其是大陆上七亿同胞之公敌；对外肆行颠覆侵略，为联合国所裁定之侵略者。目前大陆虽为毛CCP帮所盘踞，但以台澎金马为基地的中华民国政府，乃是大陆七亿中国人民真正代表――代表他们的共同意愿与痛苦呼声，并给与他们反抗毛共暴力，争取人权自由以最大的勇气和希望。所以无论根据联合国宪章原则，人道主义与自然法则，尤其是全中国人民的公意，都决不容毛CCP帮非法占有中华民国在联合国与安全理事会中之席位。<br />
<br />
　　一九四四年我国参与敦巴顿橡园会议，签署联合国宣言，以及后来参加金山联合国国际组织会议并制定宪章，其目的在“欲免后世再遭今代人类的两度身历惨不堪言之战祸”，为达到此项目的，乃规定了联合国的宗旨与原则，相期共同信守。讵本届联合国大会自毁宪章的宗旨与原则，置公理正义于不顾，可耻的向邪恶低头，卑怯的向暴力屈膝，则当年我国所参与艰辛缔造的联合国，今天业已成为罪恶的渊薮；历史将能证明，中华民国退出联合国的声明，实际上就是联合国毁灭的宣告。<br />
<br />
　　中华民族的文化传统，是坚持正义、爱好和平，现在我国虽已退出我们所参与艰辛缔造的联合国，但是我们今后在国际社会中必当仍以联合国宪章之宗旨与原则为准绳，继续为维护国际间公理正义与世界和平安全而勇毅奋斗。<br />
<br />
　　在此，我要严正声明：恢复大陆七亿同胞的人权自由，乃是整个中华民族的共同意愿，乃是我们决不改变的国家目标和必须完成的神圣责任。中华民国是一个独立的主权国，对于主权的行使，决不受任何外来的干扰，无论国际形势发生任何变化，我们将不惜任何牺牲，从事不屈不挠的奋斗，绝对不动摇，不妥协。<br />
<br />
　　我要警告世界人士，在过去短短的半世纪之中，这个世界早已发生两次惨不堪言的战祸。第一次世界大战创巨痛深之后，大家为了避免再遭战祸，乃组成国际联盟，期以维护世界的正义与和平。后来因为有些国家慑于侵略者的威胁，以为向邪恶低头，向暴力屈膝，便可换来屈辱的和平，其结果国际联盟因而瘫痪瓦解，不能发生制裁侵略与维护正义的作用，于是第二次世界大战随之爆发。今天有些民主国家竟随同附匪集团，牵引毛CCP帮非法占有中华民国在联合国与安理会的合法地位，其想法与做法，几与第二次世界大战前夕若干国家的情况完全相同，必将造成非常严重的后果。历史的事实告诉我们，维护正义的道德勇气，乃是世界安全和平的坚固磐石；而强权政治的“霸术”运用，则是走向战争的道路。<br />
<br />
　　同胞们：我们国家的命运不操在联合国，而操在我们自己手中。 国父说：“存在之根源，无不在于国家及其国民不挠独立之精神，其国不可以利诱，不可以势劫，而后可以自存于世界。”<br />
<br />
　　今天我们革命基地所拥有的人力资源与经济力、军事力和支持这两种力量的精神力，尤其是大陆上七亿反共人心与国外一千八百万反共侨胞的爱国心，无论在亚洲和世界，中华民国绝非可以任人支配出卖的弱者；而且我们对于改变世界均势与决定人类命运，实具有极大的影响力；所以大家不可只知别人的行动可以影响我们，应知我们的行动实可以使这个世界发生重大变动。<br />
<br />
　　当前的国际形势虽然很险恶，但是，只要我们自己自强不息，便没有任何力量可以使我们动摇；只要我们自己勇敢振奋，便没有任何力量可以使我们屈辱；只要我们坚忍奋斗到底，最后一定成功。大家应知，自来革命的胜利果实，都是从巨变之中孕育产生出来的。<br />
<br />
　　大家尤其要认清，现在世局的变化，乃以中国问题为中心；而中国问题如何解决，亦将决定整个世界人类的命运。所以我们在这个大变局中，实处于无比重要的关键地位；而我们奋斗的成败利钝，也将决定世界的安危与人类的祸福。我们决不静观或坐待世局的变化，一定要争取主动，掌握变化，积极奋斗，制敌机先。<br />
<br />
　　二十年来，匪帮不断发生夺权斗争，近且愈演愈烈，实可充分说明Mao思想与共产制度均已完全破产。大陆人民包括大多数共党干部，已由失望而实行反抗，毛CCP帮眼看脚下的反共火山要爆发，镇压不了，无路可走，遂改变对外战略，欲藉诡诈欺骗，苟延残喘；实则毛CCP帮决不能改变它“反苏修”“反美帝”与“反一切反动派”的目标。故其对外战略的改变，更使名在思想路线与政策路线上进退失据，造成更严重的纷歧混乱更剧烈的夺权斗争；从而将使大陆上的反共势力与抗暴斗争乘机加速扩大起来。面对这种形势的转变，所以我们更要坚定信心，充实力量，强固战备，俾能迅赴事机，加速大陆上反毛反共革命斗争的燎原之势。<br />
<br />
　　同胞们！反共斗争的行程，正如在风云变幻莫测的海洋中操舟前进，只要大家对于反共的基本形势，都有共同的认识，不为一时的变局所迷惑，紧紧把握正确的方向，精诚团结，协力同心，祸福相倚，甘苦与共；在风平浪静时，不松懈，不苟安，不骄惰；在暴风雨来袭时，不畏怯，不失望，不自欺：形势愈险恶，我们愈坚强，愈奋发，必可很快到达彼岸，拯救同胞，光复大陆。<br />
