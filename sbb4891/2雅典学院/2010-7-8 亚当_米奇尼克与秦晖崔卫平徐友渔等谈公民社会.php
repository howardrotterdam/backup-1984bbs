													<h2>亚当&#8226;米奇尼克与秦晖崔卫平徐友渔等谈公民社会</h2>
						
						
																											<div id="postmessage_728295" class="t_msgfont">许志永正在Twitter上直播 <a href="http://twitter.com/zhiyongxu" target="_blank">http://twitter.com/zhiyongxu</a><br />
<br />
转载如下：<br />
<br />
（米奇尼克用蹩脚的英语自我介绍。崔卫平老师简单介绍他，讨论社会转型公民、知识分子的作用，法语翻译。）<br />
<br />
米奇尼克：感谢盛赞介绍，我们完成的是群体工作，我只是其中之一。期待各位的问题。<br />
<br />
戴晴老师来到会议室。秦晖提问：工会在波兰起了重要作用，格但斯克造船厂私有化谈判过程很长，有人认为应该像中国一样党来送人效率高，国企冰棍效应，得赶紧送人，波兰人会觉得这样做好吗？<br />
<br />
米：首先格但斯克作为船厂是一个特别的企业，它是新自由的开幕式，80年代罢工缔造了团结工会。实际上该厂工人领袖也成了工人运动领袖。<br />
<br />
米：当时的工会领导人放弃了重组建议，因为重组要大规模裁员，市场经济和地方工会独裁之间存在冲突。这个问题很关键，不在于谈判过程是否党控制局面，我们要有新的逻辑，我们已不是独裁下的奴隶，但工会领袖没有改变思路。实际上私有化本身就充满冲突，共产主义下经济是人工控制的，格但斯克是特例。让我们假设：如果工厂造列宁雕像，进入市场经济以前，该厂生意很好，都要放雕像，一旦独裁结束，雕像市场就结束了，工人想重构自己的市场，比如造保罗二世，但工人没有好的市场思路。<br />
<br />
秦晖：中国私有化进程很快，东北一大企业三下五除二把六成以上的工人赶走了。有人说这是中国奇迹。不是说没有勇气批评中国，中国的问题确实不一样，中国太大，必须要了解现状历史，民主是一个复杂的进程，一方面腐败丑闻、压制，另一面也看到城市、商业的巨大变化，所以我应该谦虚。<br />
<br />
秦晖：不是我表态批评政府与否，确实复杂，蛮不讲理的办法操作会把问题简单化，短期内会促进经济增长，但长期令人忧虑。<br />
<br />
米：转型肯定会有代价的。经济发展如果没有同时进行的民主，经济能走多远这是关键。俄罗斯和波兰政客说，中国经济成功，因为限制自由，中国的演进也影响我们。<br />
<br />
米：有人说，中国有自身价值，民主不需要的，你们这些外国人不了解我们中国的制度。我认为，人就是人。中国企业主看来很舒服，没有罢工也没有团体诉求，但中国还有知识分子，还有要求。<br />
<br />
张大军：捷克和波兰不一样，捷克是知识分子哈维尔成为总统，波兰是工人领袖成为总统。波兰民主运动中，知识界和工人互动历程怎样的？波兰和捷克的差别是什么？<br />
<br />
米：波兰有天主教因素，很有力。自由运动三大因素：知识界、工人和天主教。历史上讲，波兰工人运动都是自发的，比如60年代，没有政治上的结果。1975 年工人自发反抗之后，大学生知识分子开始支持工人，自由欧洲电台的帮助，使得知识界对工人运动众所周知，这是一个重要的信息体系。知识界的宣言通过该电台告知全社会。<br />
<br />
米：当时有一些大众运动，比如70年焚烧党的委会，知识分子出来说，不应当去烧委员会（？），应该促使共产党建立人权委员会。联合起来与独裁斗争。对知识分子而言，民主的问题是自由问题；对天主教而言，他们要捍卫的国家宗教传统，曾经被共产主义摧毁；对工人而言，为争取更好的工作条件，也就是工作上的解放。独裁终结也意味着三种联合的终结。<br />
<br />
米：我曾经团结在瓦文萨周围，专制结束之后，瓦认为是后专制时代国家的未来，而我认为，是一个民主国家的未来。实际上结束专制之后，最大变化是，我们希望建立人性化制度，而专制结束后成了资本主义。三种力量团结已经不存在。<br />
<br />
米：今天上午我和崔女士就共产党转型中的角色很重要。我们认为只有依靠党内改革派才能实现非暴力转型。<br />
<br />
米：第二个问题，必须创造这样的条件，转型后旧的共产党人应该能找到他们的位置。如果我们歧视共产党人，将来就会有这样一股势力反民主。<br />
<br />
郭玉闪：接到邀请要去波兰参加一个会，他们（秘密警察）知道了，不让我去，我说要去。后来他们说可以走，我办了签证，但那天他们过来阻拦我。警察和保安看了我一个晚上，不让走。<br />
<br />
米：虽然我不是外交部长，我可以再次邀请你。<br />
<br />
戴晴老师介绍传知行研究所。和波兰不一样的是，中国原有的宗教系统被摧毁了，他们和宗教联系，主要是基督教。<br />
<br />
郭玉闪：请更深入分析波兰共产党。他们愿意和团结工会谈判，选举失败了也接受。而中国89年党拒绝谈判。波兰共产党愿意放弃暴力和专制，它和别国共产党有什么区别？<br />
<br />
米：两个回答。第一，1981年12月13日波兰党已经戒严了，民主党派领袖约8000人被逮捕了，这是决定性时刻，当时的问题是，党能否摧毁寻求自由的运动？回答是不可能的。有一点是清楚的，武力是一条无效的道路。和中国现状不同，当时我们经济上毫无成就，只是不断危机。当时处境之下，波兰背后没有红军做依靠了。如果镇压，他们必须依靠苏联红军，共产党政权太薄弱了。党不能清算反对派，反对派也不能推翻党，两者都太薄弱，不得不开圆桌会议妥协。之前经历了漫长的压制。波兰民众当时都收听自由欧洲电台。<br />
<br />
秦晖：和中国比，波兰共产党实在太好太好了。<br />
<br />
米：尽管波兰共产党让我坐了六年牢，我还是同意你的说法。<br />
<br />
米：中国经济很成功，这不应该只是共产党的结果，而是全社会共同努力的结果。要面对全社会的中国，每个人都爱国，都想成为这个国家的公民，官本位时代应该过去了。问题是怎么找到对所有人都有益的规划？要准备事实上的多元存在。比如地方政府想建一个汽车厂，我们应该就此给出看法，如何组织劳动者的团结，可以从小型的组织开始，比如帮助下岗工人，可以组织自由的避风港比如独立的研讨会。组织不受国家控制的社会学历史学课程。在村一级组织选举，使得民众可以管理地方事务。这就是公民社会建构进程。在循序渐进建构民主的反对派，应该是当权者的合作者。实际上专制采取的策略是把社会分裂原子化，我们的策略应该是联合，面对政权的独立力量。今天中国的演变对于全球每个人都至关重要，因为这关乎到民主是否人类的未来。以新加坡为例，他们认为民主不必要。最后的答案在中国。<br />
<br />
徐友渔：当你们争取民主的时候，苏联曾是最大障碍，但是目前两者共产主义都结束了，为什么波兰还是把俄罗斯作为头号敌对国家？波兰民主派是否支持官方的外交政策？<br />
<br />
米：我真正意义上反苏联，虽然我有很多俄罗斯朋友。虽然苏联共产主义终结了，但俄罗斯帝国主义还在，这个传统是扩张式的霸权主义，过去十年中，普京政府在重构帝国主义政治，不是独裁的而是专制的政府。我的俄国朋友说，我对普京的做法太容忍了，他们认为普京是俄国民主的终结，我不这么认为，我觉得俄国目前也面临重大选择。<br />
<br />
米：俄罗斯只要能看到邻国弱势，他们就要创造冲突点，目的为了控制邻国。俄罗斯的思维：虽然波兰不是他们的了，但乌克兰的基辅还是他们的啊。<br />
<br />
米：也许不恰当，对于俄罗斯而言，乌克兰的地位正如西藏对于中国。<br />
<br />
米：俄罗斯民粹主义认为，乌克兰本身是不存在的。乌克兰国家的概念对于俄罗斯沙文主义者来说就是挑衅。但现在，目前没有人打算修订边界。我们认为应该开放边境，而不是改变边境。<br />
<br />
米：顺着这个思路，中国政府有必要和达赖喇嘛协商。达赖要的是自治而不是分裂。我亲自和他有过讨论，他亲口说，他摒弃暴力，但如果对话没有空间，西藏年轻人很可能除了暴力之外没有出路。<br />
<br />
戴晴老师：普京怎么看中国？<br />
<br />
米：对于俄国专制者来说，北京的体制太理想了，已经是他们嫉妒的对象了，我在莫斯科亲耳听到过，戈尔巴乔夫很羡慕李鹏，李鹏做的多么彻底。<br />
<br />
戴晴：会不会有一个强大的反民主阵营形成呢？<br />
<br />
米：会的。<br />
<br />
米：重要的不是他们俩，而是梅德韦杰夫周围的团体和普京周围的团体，两种团体不一样，普京周围是克格勃，他们建议中国的道路，梅德韦杰夫周围代表改革派，他们希望民主改革，步步为营，但最终走向民主。<br />
<br />
米：俄国正在展开讨论。梅周围的人，在想到底怎么推动民主改革，他们已经通过宣言，在努力。<br />
<br />
陈贯中：很多人到东欧投资，波兰怎么看中国的投资看这些中国人？<br />
<br />
米：直到今天，还没看到这中间存在冲突，未来还要观察。我个人认为，华沙如果有一个中国城，那会很好。问题是，东欧人不了解中国现状，精英们看到的只是中国经济成功。<br />
<br />
梁晓燕：中国经济发展在欧洲被关注，东欧知识分子有没有忧虑，中国经济发展可能改变世界规则？<br />
<br />
米：他们还没有。我不这么看，我认为，波兰知识分子还在观察中，当然他们还是很同情中国民主派的，但是，对于中国过去20年经济成就，他们不可能不知道，现在还不是他们害怕的时候，而且中国那么遥远，我们只恐惧俄罗斯。<br />
<br />
害怕软实力吗？米：坏实力是hardpower软实力是好的power。<br />
<br />
米奇尼克接了个电话，说抱歉他是主编，忙着编务，自由的报纸也是民主的成果。<br />
<br />
郭玉闪：几种民主力量知识分子工人天主教容易冲突，刚才讲到民主推动几种力量，怎么合作的？尤其是遇到原则分歧的时候？<br />
<br />
米：这不是机构的联合，只是道德的联合。分歧是自然的，我们反对派中也有不同的倾向，首先，比如激进还是温和立场，这是第一大分歧，<br />
<br />
米：还有，不知道是无情打到这个制度呢，还是要意识到，我们共同是波兰公民。在我们的民主反对派内部，是否应该多元，还是要人为制造团结面对强大的力量？<br />
<br />
米：分歧自然，没有哪个反对派势力一支团结到最后，我们必须接受这种现实。但我们要采取谨慎态度，我们的任何分歧都可能会警察利用。<br />
<br />
郭玉闪、崔卫平：戒严后反对派之间出现裂缝，后来如何弥补的？非暴力如何形成共识的？<br />
<br />
米：当然，戒严之后，反对派内部有多种倾向，很多人甚至要诉诸武力，比如一些地下活动领袖。一方面得益于工人运动领袖，他们服从现实力量，团结工会的一贯理念都是非暴力，之所以非暴力一个直接原因是我们的力量太薄弱了。<br />
<br />
米：如果不能通过非暴力和解，也为苏联介入提供理由。一直到圆桌会议之前，社会是分裂的。<br />
<br />
米：只要民间社会采取暴力，就会激起政府暴力，最后的结果是内战。关于裂缝弥合，和解之所以产生是因为政权和民主派都薄弱。<br />
<br />
米：共产党中很多人看到，改革是走出危机的唯一道路。和解是一个心理层面的问题也是政治层面的问题，二十年后，这种进程还没有结束，我们必须让时间抚平一切，对于今天30岁的年轻人来说，那已经是传说。<br />
<br />
米：和解的困难。对于某些群体而言，还没有忘记遭受的压迫，他们坚决要求正义，我理解这些心理，但对于波兰的稳定而言，假如采取暴力纲领，那将是致命的危险的。<br />
<br />
米：报复性做法是致命的，有损社会稳定。如果让他们对从前的行为负责，只需要说明他们做的不公正，但没有必要报复。<br />
<br />
南卡政治学博士生：研究表明，不存在纯粹的民主，用数学验证，不存在总的集体意志，民主只是制衡。您和瓦文萨的立场分歧，您说要建立纯粹意义上的民主国家，这将是一个什么样的国家？<br />
<br />
米：绝对的民主不存在，正如绝对的独裁不存在一样，当然纳粹是例外。<br />
<br />
米：我不再赘述我和瓦文萨的理念冲突。我认为有两种民主，一种是普选式的，一种是议会式的。要看到，独裁和民主之间肯定有区别的。<br />
<br />
戴晴：请您谈谈对21年前天安门悲剧的看法？<br />
<br />
米：这是一个让我感到冲击的问题。刚才讲到，没有纯粹的民主和独裁。89年中国一方面是无暴力的反对派，另一方面是专制政府的残忍屠杀。能思考的知识分子都会看到，当时是猫和老鼠的关系，应该这样界定，那是独裁的自我防卫。<br />
<br />
米：天安门的事实被承认，这将是中国民主的出发点。<br />
<br />
崔卫平：许志永在twitter直播，我们每个人都是一个自由电台。直播结束，共聚晚餐。感谢大家！</div>

							
							
							
							
