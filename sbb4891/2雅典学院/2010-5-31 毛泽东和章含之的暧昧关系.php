													<h2>毛泽东和章含之的暧昧关系</h2>
						
						
																											<div id="postmessage_649792" class="t_msgfont">捅破毛泽东和章含之暧昧关系的窗户纸 <br />
<br />
李大立　　2008年07月13日 <br />
&nbsp;&nbsp;　&nbsp;&nbsp;<br />
(题注：引王维「西施咏」诗，唯「娇」字改「骄」)０四年春明报连载洪君彦先生「我和章含之离婚前後」，三天後突然腰斩，停稿启示说应女儿洪晃的要求，当时令许多读者不解之馀引颈盼续。三年半後洪先生终於出书，总算有个交代。笔者对婚姻私隐向无兴趣，哪怕是「天下第一婚」，喜从不同的视角观察社会。在此就洪先生大作(以下简称洪书)发表一些个人意见，恳请洪先生及广大读者指正。<br />
<br />
1963年毛泽东七十大寿前几天，毛泽东请了程潜丶叶公绰丶王季范和章士钊四个湖南老乡赴家宴，邀请时特别说明，不带夫人可带一名子女。这种邀请本身就不合礼数，不符合民族传统，因此现时很多分析家认为这是毛泽东设下的局，想藉此私会章士钊女儿章含之不无道理。据说十三年前，1950年国庆节，那时章含之才十五岁，随父亲去中南海，已第一次见到伟大领袖。以洪书所说，章十四岁已懂得恋爱，写情书给他，所以很可能那时已给毛泽东留下印象。这次不同了，邻家有女初长成，章已是二十八岁的青春少妇，在素喜女色的毛眼里，无疑见猎心喜，於是，就提出了请章做「英语老师」。<br />
<br />
本来，一国之君要找个家庭教师教英文有何不可？问题是发生在毛泽东章含之身上，就显得不寻常了。首先，毛身边已有英文教师林克丶其保健医生李志绥曾在澳洲行医，师资绰绰有馀；退一步讲就算要换新口味，偌大一个北京城，什麽高级英文人才没有？非要找一个毕业没有多久，教学没有多少经验的女青年？可见毛泽东醉翁之意不在酒。其次，以毛泽东的出身学历特别是外语资质，根本就不合适学英文。毛是一个超龄学生，十七岁才开始接触英文字母，二十五岁才中专毕业，大半生戎马倥偬，根本就没有英文基础，自称「西瓜大的英文字母也只认识一箩筐」；再加上资质迟钝，乡音不改，五音不全，「学英文」不过是一个不高明的藉口。用章自己的话说：「许多年之後的今天，我才意识到那时毛泽东大概处低潮的思考时期，他找我学英语是一种消遣」。「章含之有一次还告诉我，毛泽东曾想带她去外地，住在他的专列上，她教毛泽东英语，毛泽东给她讲历史。这个计划如此诱人，使章含之非常激动地期盼着这一天。」(明报月刊08年3月箫关鸿「跨过大红门的章含之」，以下简称箫书)以後，章小姐经常在周末去中南海单独和毛泽东吃饭，以毛泽东可以和女青年伴舞入房「休息」数小时的往绩，他和章之间发生过任何事都不足为怪。网上流传的「毛放安眠药，强占奸污......章含之含着眼泪告诉了自己的丈夫。」(见「看中国」07年3月17日「章含之女士出书揭露与毛泽东交往」)当然不可贸然相信，但在当事人没有否认之前，存疑还是合理的，这就更加需要当事人有勇气面对历史和事实，给全中国人民一个交代。<br />
<br />
毛泽东的「学英文」学了半年无疾而终，两年以後文化大革命爆发，洪君彦先生在北大首当其冲，被批斗游街剃阴阳头，女儿吓得大哭，「她(章)坐在沙发上，双脚跷在茶几上，用鄙夷的口气说：&quot;你看你这个死样子，还有脸回来啊！现在人不像人，鬼不像鬼，我要是你，跳到北海死了算了。」(见洪书)然後是章「革命大串联」，在上海红杏出墙，洪「牛棚改造」，下放外省劳动，在心情极度孤独忧郁的情况下「陷入婚外情的泥沼」。「在文革中，知识份子都成了&quot;臭老九&quot;，发配到农村干校劳动改造，但是毛泽东没有忘记章含之，一纸命令，把她从农村调回北京」。(箫书)1971年章含之进外交部当翻译，陪同乔冠华出席联合国大会出尽风头。接着就发生了1972年9月中南海那段着名的毛泽东离婚最高指示：「我的章老师，今天我要批评你，你没有出息......」，「在场的自周总理起的几个中央领导都楞楞地看着这戏剧性的一幕」，章当场哭了起来(见章「十年风雨情」，以下简称章书)。1972年，正是「文化大革命」後期，又正是林彪杀毛未遂，毛「精神上受到致命一击」复元後不久，毛泽东身边有多少患难与共的战友还被整得死去活来，国民经济到了崩溃边缘，作为日理万机的一国之君不去关心一下，却对一个普通翻译的家庭婚姻了若指掌，并且不顾场合直接干涉，说明什麽问题？<br />
<br />
跟着就是奉旨离婚，「就在同一天深夜，实际上是第二天凌晨，急促的门铃把我惊醒，毛主席......派人送来一筐红苹果，来人说主席祝贺我自己解放自己了。」(见章书)二十三年夫妻一朝散，正常人就算不留恋悲伤起码也会心神不定，可是章办了离婚的当天，伟大领袖就迫不及待地派人送红苹果致贺，到底居心何在？可惜毛一世英明，精於计算，猴急之时忘了有福独享，发表逼离最高指示之时，正是外交部汇报与日相田中角荣会谈结果之後，刚丧偶的「外交部长」乔冠华在座，事後乔即问章「主席那天说的是真的吗」？螳臂捕蝉，黄雀在後，随後乔不顾不伦之恋，展开热烈追求；章本来就爱攀高枝，结果一拍即合，转投乔冠华怀抱，开始一场轰轰烈烈的忘年恋。不料毛知道後，「很失望和生气」，立即派人来向章发出警告：「说毛主席鼓励我(章)，祝贺我解放自己，是希望我为他(毛)好好工作，没有让我马上跳上乔老爷的船谈情说爱，同乔结婚。」(见章书)为什麽毛泽东如此不顾身分，不顾辈分，从教唆逼迫章离婚，到警告章不要和别人恋爱？<br />
<br />
为达目的，毛泽东竟然不顾一切，屡施小计，先在73年6月要调章去加拿大当「新中国第一个女大使」遭到章的坚决拒绝，毛也不便来硬的，以免太过露骨，但章知道毛不高兴(要知道当年一般百姓敢违抗最高指示就是死罪)。一年後的一天在见完外宾後，毛叫章随他到休息室，激动地对她说：「你不听我的话，你心里没有我！」章紧张得不知如何回答，只好说：「主席，你这样说，我承担不起！全中国人民心中都有你，我哪里敢心中没有你！」(见章书)，为了拆散章乔，作为最高领袖，毛绕过国务院丶外交部直接指派章当女大使，一计不成，竟然不惜说出「你心里没有我！」这样的话来，哪里还有半点像「伟大领袖」？简直像一个调戏良家妇女的西门庆！至此毛泽东已是不顾一切孤注一掷了。章乔在纽约热恋时，毛曾指使其外甥女王海容急电章即回北京，章请求乔出面说情「北京有什麽大事情非要我这个小翻译不可？」，反被王「大发雷霆」，辗转回北京後，「毛主席一句话把冠华和我打进了十八层地狱。」(见章书)<br />
<br />
至此，洪书中质问的：「到底谁是婚变的始作俑者？到底谁是婚变的主角？」已经一目了然。综观洪章及女儿洪晃三人所说，尽管各有不同，婚姻破裂的大致轮廓已经清楚。就算後来的章乔悲剧，最根本的原因都是政治，始作俑者都是毛泽东。洪先生身为一个经济学教授，目睹五十年代毛一个接一个倒行逆施，不但没有被打成右派，反而入了党，不可说不紧跟了，到了文化大革命还是被抛了出来；章小姐不但没有丝毫的同情，反而口口声声要「划清界线」，当「革命派」，加上和毛的私人关系，到了文革後期还是和「外长」被双双打入冷宫，可见毛玩的是完全没有规则的政治游戏。洪章的婚姻悲剧，不过是极度扭曲的社会所造成的千千万万婚姻悲剧之一而已。<br />
<br />
洪先生说：「我已年过七十了，有责任还历史的本来面目，历史作不得半点假，真理也不容捏造歪曲。」可是上述这些历史事实，作为当事人洪先生不可能「懵然不知」(洪语)，可是在书中却只字不提。如果洪先生仍在大陆，大家均可理解；可是洪先生已在香港定居十几年了，不能不令读者感到洪先生仍然「为尊者讳」。读者之所以对你们这一段婚姻感兴趣，绝非因为你们是甚麽「金童玉女」，而是因为它反映了政治，反映了文化大革命，更反映了统治中国二十七年的暴君毛泽东个人品格，对研究中国大陆的社会悲剧及其成因很有价值。正如附录中司徒华先生所说：「家事总打上国事的烙印，一些&quot;家丑&quot;往往是&quot;国丑&quot;的产物」，所以笔者认为洪先生更有责任在有生之年将历史的真相留给後人。另外，笔者觉得洪书中还有「为自己讳」的地方，令洪先生一再声明的真诚坦白打了折扣。比如附录中洪晃说：「我爸结过三次婚，我也整整三次，还在比他小得多的情况下，就把这三次都结完了。」且洪晃另有专文讲及继母朱一景，可是当事人洪先生却避而不提，洪书中只写了第一丶三次婚姻，只字未提第二次，只是用「未能克服感情冲动，步上了婚外情的歧途」代替，不能不使读者感到有所隐瞒。<br />
<br />
此外，平心而论，洪书中所说大姐在上海家中发现章与张同事「不轨行为，并掌握确凿证据」以及後来洪在北京家中搜查章手袋，发现张某照片和安全套，愤怒极了，争吵起来章突然下跪认错，这些情节缺乏说服力，不排除有误会和冤枉的可能。而洪先生对朱一景的恋情虽然讳莫如深，但承认自己「犯下了婚外情的错误......竟然干出这种事情来。」反是确凿的事实。书中说：「章怒气冲冲责问我，我理直气壮地说：&quot;是啊，现在我和你一样也有了外遇。不过你做在先，我做在後，你做的是暗的，我做的是明的，咱们扯平了，谁也不欠谁。&quot;说得她哑口无言。」窃以为这里也有疑问。如果章真是早已红杏出墙，负人在先，她怎麽能如此理直气壮「气冲冲地责问」？是否真有可能先前她的不轨全是误会？而且洪先生这番「理直气壮」的「在先在後，明的暗的，扯平不欠谁」的外遇论，也似乎有失大学教授的身分。洪先生这麽「理直气壮」一下，以此作为报复，读者的同情心一定大减。<br />
<br />
洪书写到「想不到办离婚也有特殊化，也可以走後门。」不但有趣，而且可见当时的社会多麽黑暗：1973年洪章第一次去居民委员会办离婚，居委会负责人冷冷地盘问离婚理由，双方答不出没有办成。两星期以後，章叫洪再同去一次，「她说，这次成了，可以办成」(见洪书)。同一个负责人倒茶递烟热情接待，一句话不问就办好了，原因是「上头」有人打了招呼：不许问长问短，照办则可。联想到上面章书所说的毛在当晚即派人送来红苹果，两书对照，怎麽回事不言自明。洪书说到章乔结合为了「少一层阻力」，以外交部名义把十二岁读小学的女儿洪晃公费送美国「留学」，读者对当权者予取予携的阶级特权不是又多了一层认识了吗？可是事後还有人对此津津乐道感到无上光荣，可见共产专制毒害之深，即使是受过西方教育的人也不例外。<br />
<br />
至於章小姐，洪书已经为我们刻画出一个典型的活脱脱的「新中国女青年」，除了毛式政治，向上爬，爱虚荣，一切伦理道德都在这一代人身上消失殆尽。据洪书说，他被北大造反派剃阴阳头的那天，章双腿搁在茶几上说出了那一番令人心冷的话来，「我第一次在亲人间感受到人情冷暖」。换句话说，直到文化大革命之前，夫妻还是恩爱和睦的，因此网上流传的章被毛奸污回家向丈夫哭诉，也就有了成立的理由。众所周知共产党的政治运动往往是一风吹，仅仅是因为丈夫被批斗，被剃阴阳头就如此冷漠无情，寡情薄义，说明这个女人多麽狠心。跟着发生的与张同事暧昧关系，剪掉结婚相，「我要和他划清界线」 ，对丈夫不理不睬等等就很自然了。可叹的是离婚後章又要求洪仍然回家住，在女儿和岳父母面前做戏，可见她爱面子的程度。<br />
<br />
附录中其他人的文章所说就更显露章小姐的品质了，比如说自称头上有四大光环：「总督孙女丶总长女儿丶主席老师丶外长夫人」。说者含糊其辞，有心者却认真翻查。所谓「总督孙女」最可笑，有传言说指其养父章士钊的元配夫人吴弱男的祖父吴长庆曾当过清浙江水师提督，而吴章早於1929年离婚时，章含之还未出生，更未被收养，真是「八杆子也打不着」。即使如此应是「提督曾外孙女」，和「总督孙女」相去千里；据章含之出版经纪人箫关鸿说：「她亲生父亲的父亲是安徽总督」(见箫书)，因此又有传言说指其生父陈某之父陈调元军阀混战时期当过安徽都督，若如此也不过是「都督孙女」，两者皆非「总督孙女」。反有人提出证据说章小姐生父是上海法租界巡捕房华人翻译薛耕莘，与永安公司「康克令」小姐珠胎暗结，对薄公堂，两个都不要孩子，律师章士钊说：「你们都不要，我来要吧」。连自己的生父是谁都不知道，或者不敢公开，就拿什麽「总督孙女」炫耀，何其轻薄！说到「总长女儿」，章小姐就更应感到羞愧才是：1953年章的同母异父哥哥找上门来，章才知道了自己不是章士钊的亲女儿，是一个上海交际花的私生女。觉得受了欺骗，把怨恨全记在章氏夫妇身上。加上章士钊是旧官僚，为了追求进步，章小姐写信给彭真要求脱离养父子关系。彭真派秘书劝告她：「他们一直把你当亲生女儿对待，从来没有虐待过你，不要偏激，做出不合符政策的事情。」四十多年後反过来又以此作政治光环了，羞耻心何其凉薄！其他的所谓「主席老师」丶「外长夫人」怎麽回事，大众都知道了，再拿出来炫耀就显得太可笑了。大陆社会最不堪的是动不动就自称「天下第一」，即使是受过教育的章女士，不但语焉不详地往自己头上戴光环，还大言不惭地称其後夫乔冠华「Top of the world」(关於乔冠华的外交作为请参阅04年11月「议报」205期拙文「所谓外交突破，全是自吹自擂----浅评中共五十五年外交之二 」)，其浅薄无知不但贻笑国内，还献丑国际。所谓「最後贵族」丶「末代名媛」尚且如此，大陆社会整体道德水平更可想而知了。<br />
<br />
其他附录文章说到的诸如：乔章为了赶走同住的乔子女，偷偷换门锁，子女们不得其门而入，不得已数天後趁他们不在家时搬走自己的物件。章居然致电公安部长说「外长家发生一起重大政治事件」要求拘捕乔的子女；又致电乔女儿所在单位，要求取消她升大学资格等等，这些作为能和「名媛」丶「贵族」挂得上钩吗？恐怕连市井小民中的好後妈都不如。就和给毛主席写告密信丶乔冠华1973年在中共政治局扩大会议上上纲上线狠批周恩来，两年後周恩来病重乔又当面请求周原谅一样，都是私心大暴露的政治投机。看来章乔两人最後走到一起不是偶然的，有其共同的政治品格基础。正如附录中李捷先生「长恨人心不如水」中所说，无论洪章或者章乔的婚姻都是「时代的悲剧丶政治的祭品」。特别是章乔的政治婚姻，「一开始就与各种权力斗争纠缠在一起，权力使人疯狂，他们孤注一掷丶拚命一搏，其结果必然是满盘皆输政治自杀。」正当章小姐「跨过厚厚的大红门」在大陆红极一时，请李慎之先生为乔写纪念文章时，李先生批评说：「章含之的书很糟糕，他们这对夫妻关系之好，一个苹果一个梨(不知是否臭气相投的意思？--笔者注)......他们对文化大革命丶对周围的朋友的不幸无动於衷！」<br />
<br />
章小姐哀叹：「很多年过去了，当二十年前的一切都已成历史的陈迹，当我失去了冠华，失去了当时盛极一时的事业，只剩下我孤零零的独自一人时，我也曾饱含辛酸地回首当年，不知道如果二十年前，我做了另一种抉择，今天又会如何？残酷的命运似乎从我降生之日起就开始捉弄我！」命运并没有捉弄她，她得到一个好养父母，已经比劳苦大众普通人家的子女优越多了。是她辜负了命运的安排，饮下了太多的狼奶，丧失了太多的人性，作出太多的政治投机，所以才有「孤零零的」今天。就像附录耿一刚先生文章中所说的冰心女士，当丈夫落难的时候，理解同情他，全力支持他，在周恩来年前慷慨陈词：「我的思想和文藻完全一样，如果他是右派，我也情愿当右派！」逼使周恩来无言以对，最後说了一句意味深长的话：「这时候最能帮助他的人，只能是他最亲近的人了。」作者说：「以冰心，一位正常的丶正直的妻子，观照章含之，什麽也不要说了。」而当曾经互相一见锺情的前夫洪君彦落难时，章小姐却相反作出了「划清界线」的选择；即使是对爱得死去活来的後夫乔冠华，章女士也屡屡提及：「因为我有着很强硬的&quot;关系&quot;，我只要不受他（乔）的牵连，我的事业会很顺利。」(见章书)可见在章小姐心目中，无论前夫後夫，无论青梅竹马还是忘年之恋，统统都不过是政治斗争的工具。难怪「外交部里的人说起章含之来，无不摇头，都说她只是嫁了乔冠华，若嫁了毛泽东，肯定比江青还刁恶。」(见马悲鸣先生「章含之，毛泽东任人唯亲又一个样板」) 司徒华也说：「我总的感觉是：章含之是一个小江青，是一个最终未能得逞的江青」(见司徒华「章含之前夫洪君彦」)。笔者可以肯定地说，若果二十年前她作出了另外一种选择，文化大革命风暴过去後丶阴谋拆散别人恩爱夫妻的毛泽东死了的今天，她一定还和洪先生和女儿共同拥有一个劫後重生的幸福家庭。彷佛冥冥之中上天作了最公平的裁决，洪书中说：「我的晚年，过得既幸福又安定」，对比章上面的哀叹，很可能道理在洪先生这一边。不要不信命运，章小姐最崇拜的伟大领袖毛泽东自知作恶太多，就曾经在庐山会议上哭诉：「始作俑者，其无後乎......我是始作俑者，我无後乎！......」世人当以为戒。<br />
<br />
(後记：本文作於章含之女士过世前，未及在她生前刊出。对於章女士的去世，笔者深表哀悼，本欲撤稿，经编辑审定认为仍有出版价值，笔者已略作修改，好在对事不对人，望大家汲取教训。至於本文呼吁章女士对历史负责，公开与毛交往的真相，也就只有永远石沉大海了，由此也可见抢救反右文革历史的迫切性--笔者注)</div>

							
							
							
							
