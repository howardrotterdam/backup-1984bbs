&nbsp;&nbsp;“我和冠华的感情逐渐在外交部领导层中有所透露，很快地，我受到了极大的压力，我的“朋友”外交部的“通天人物：向我发出了警告，说毛主席鼓励我，祝贺我解放自己，是希望哪个我此后能为他好好工作，没有让我马上跳上乔老爷的船和他他谈情说爱，同他结婚。” <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp; &nbsp;章名媛和毛泽东关系密切，也不是什么新鲜事了，最近章的前夫洪君彦出版了一半关于他们婚姻的回忆录的副标题就取名《毛泽东亲自过问的一桩婚事》，按常人想象毛泽东这样的大人物也不会如好事的长舌妇一样关心起小青年的婚姻，俗话宁拆十座庙，不破一门婚，毛泽东如此费事的过问显然不正常，再把话说话来，引用的这段文字虽然不是原话，但是笔者判断文中指的通天人物，就是指外交部里亲近毛泽东的人，不外乎王海容，唐闻生二人，究竟是谁说的不去管她，语言风格上也确是毛泽东那种粗俗实在，可以断定这确实是毛本人对此事的态度。才子配佳人，毛泽东为什么对于章乔的结婚感到不满呢？<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;&nbsp;&nbsp;之后章名媛因感受到这种压力和乔冠华保持这若即若离的关系，但是在一次“失踪”后，她投向了乔冠华的怀抱，其实说起这样的事也很平常，任何一个恋爱过的人都会遇见。菲律宾总统访华，毛泽东晚上接见，章含之去做翻译，会谈后章整理会议记录，一夜未归。回来时值班秘书告诉她：“乔冠华从12点左右开始打了无数电话找我，说他到处找不到我，后来几次显然是喝醉了每说话都不清楚，最后一次讲了一半听筒就掉了。”章名媛来到了乔的住所，乔的保姆告诉她，昨天晚上打电话找不到章后喝了一瓶茅台。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;&nbsp;&nbsp;乔冠华作为外交部长，当是知道章含之去做翻译了，所以料想12点的时候打电话来可以找到章，这里要注意一个细节，12点没有回来显然令乔很意外，也很沮丧，开始饮酒，在饮酒又不断非理智的打电话到外交部的值班室。按说一个50岁的人了，也不至于喝点酒后那么失态的担心一个大活人吧。必是心中有什么疙瘩。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;&nbsp;&nbsp;乔见到章后，反应也颇为激烈“你不要离开我，你不要躲着我！”章好言劝慰后后回到住所，碰到唐文生，唐转告了半天乔多次电话寻章的事，作为毛安插在外交部里通天的人物，唐也参加了昨晚的会见，当是知道章在毛那的情况，乔打电话来也是情理之中。但是唐属于毛泽东安插在外交部里的耳目，乔并非不知，如此不顾部长之尊半夜去告扰私事也不甚妥当。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp; 事情脉络就这样，那么做一个假设的推理或许能解释这事中种种的不合常理：毛泽东和章含之有一种情感关系，所以鼓动章和前夫离婚，但在得知章转投乔的怀抱后，毛泽东对此非常不满。在一个外事活动后，章的彻夜未归，让乔冠华十分的担心，这里的担心显然不是人身安全上的担心，而是担心章在外事活动后留宿在毛处，做为情人十分担心这种情况才失态的多次打电话询问，在得知章在半夜未归后又嫉妒心理做怪喝醉了酒。</div>

							
							
							
							
