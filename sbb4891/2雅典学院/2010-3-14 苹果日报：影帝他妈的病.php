																											<div id="postmessage_515563" class="t_msgfont">Apple Daily 苹果日报网上版 －－2010-03-13<br />
他妈的病<br />
<br />
中共人大、政协开会前夕，温家宝上官方网站答百姓问，忽然一脸沉痛，谈到他妈妈：「去年访英，我骤逢不愉快事件，家母那天在电视上看见，脑子溢血。」温家宝据说是政坛影帝，但他编剧的工夫有时差了一点。<br />
<br />
温家宝所谓不愉快事件，无非是指去年二月二日在剑桥遭大学生扬克斥为独夫掷以便鞋。但是，去年二月二十八日，他第一次上网答百姓问，显然还没有想到，妈妈不脑溢血不足以证明母慈子孝。请看他当时的话：「家母前几天患脑栓塞，双目近乎失明。」脑栓塞变成更严重的脑溢血，当然可以倍添哀感；但把二月二十八日的「前几天」提前到二月二日，也就是「前二十几天」，未免稍嫌牵强。<br />
<br />
《古今谭概．苦海部》记载了一个「母亲脑溢血」式故事：宋朝文士李廷彦有一次向上官献百韵诗，写到「舍弟江南没，家兄塞北亡」。上官恻然叹息：「君家凶祸，一至于此！」李廷彦还老实，回答说：「实无此事，图对偶亲切耳。」当时有人戏道：「何不言『爱妾眠僧舍，娇妻宿道房』？犹得保全兄弟。」温家宝要以亲切的文艺悲剧赚民热泪，似乎同样可以出动娇妻爱妾，保全妈妈。<br />
<br />
而假如温家宝以为把妈妈年年掂出，可以劝孝，那是完全误解了我国的「孝」字。<br />
<br />
唐太宗母亲四十五岁去世，没看见儿子登基。有一天，太宗经过母亲故居，悲从中来，流泪说：「朕生于此，今母后永违，育我之德不可报。」一天晚上，他梦见母亲，音容训示无异平时，醒来之后，不胜伤悲，明旦视朝，「诏有司大发仓赈贫瘠，以为后报焉」。太宗在位二十三年，爱物仁民除邪去害，表现的就是大孝（《新唐书．后妃传》）。<br />
<br />
现在，温家宝得宰天下，说的和唐太宗大率相似，做的则无一雷同。例如前年四川地震，豆腐渣建筑物杀人无算，温家宝政府不追究合作偷工减料的官商，却把致力揭露减料偷工事实的黄琦、谭作人等一一判处入狱多年。又如骇人听闻的三聚氰胺奶粉案，遭毒害婴儿以千万计，有赵连海为受害者奔走索偿，却遭拘捕。还有艺术家艾未未，鼓吹民权，被打得脑出血。温家宝这许多政绩，恐怕和我国的「孝」字格格不入。<br />
<br />
当然，温家宝所作所为，也许都是遵从母训；也许他的确以为那就是孝道，所以，每次上网，把百姓提出的民权问题全部删去之后，就一脸沉痛，谈他妈的病。<br />
<br />
古德明<br />
