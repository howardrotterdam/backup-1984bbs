													<h2>“共和国三大演说家之一”的曲啸在美国遭遇滑铁卢</h2>
						
						
																											<div id="postmessage_618015" class="t_msgfont">“共和国三大演说家之一”的曲啸在美国遭遇滑铁卢 <br />
<br />
润涛阎 4-27-2010 <a href="http://freechoosing.blog.sohu.com/151304058.html#comment" target="_blank">http://freechoosing.blog.sohu.com/151304058.html#comment</a><br />
<br />
 <br />
<br />
&nbsp; &nbsp; 曲啸是中国1949年以来的三大演说家之一（当时的排名顺序是：曲啸，李燕杰，彭清一） 。八十年代中后期成为家喻户晓人人皆知的人物。有一部电影《牧马人》，故事里的主人公就是曲啸先生。但曲啸属于“不鸣则已一鸣惊人”以至于早一点来美的留学生不知道曲啸何许人也。当时留学生能否按时回国，是政府担心的大事，便考虑曲啸先生来美国给大家做思想工作，提高大家的爱国热情。&nbsp;&nbsp;<br />
<br />
&nbsp; &nbsp; 曲啸由国务院刘中海先生陪同来到美国给留学生做巡回演讲。但出乎他本人所料，在美国第一站就发生了令他不得不中断演讲计划的场面。这个事件导致了曲啸教授立刻心理崩溃。曲啸教授回国后也没有休养过来，不久就大脑出了毛病。几年后半身不遂，躺在床上达十好几年，直到去世。国内几乎没有人知道曲啸到底为何突然间大脑出了毛病。而润涛阎本人亲自经历了这个事件，有必要交代一下，虽然有点迟。&nbsp;&nbsp;<br />
<br />
 <br />
<br />
（一）曲啸求我讲故事 <br />
<br />
&nbsp; &nbsp;“老阎吗，我今天求你一件事，而且很急。你今晚在家吗？” <br />
<br />
&nbsp; &nbsp; 这是我们学校“中国学生学者联谊会”会长在跟我通电话……他说今天有客人来，是大使馆教育处刚给他打来的电话，说国务院派来到美国到各大学巡回演讲的二位提出要求到同学家里吃饭…… <br />
<br />
&nbsp; &nbsp; ……其中一位说：“我叫刘中海，在国务院工作。他就是大名鼎鼎的曲啸教授，是心理学教授也是法学院教授，著名的演说家，还出版了犯罪心理学专著。这次国务院派我来，主要是陪同曲教授来美国给中国留学生们做巡回演讲，我只是陪同，做个伴而已。曲教授是中央宣传部调研员。你们出来的早，没听说过他的大名，但在国内，他和李燕杰齐名，是青年人的良师益友。”&nbsp;&nbsp;<br />
<br />
&nbsp; &nbsp; ……曲啸终于听完了刘中海的介绍，然后他也介绍了刘中海，说刘中海同志可是周恩来总理的秘书，工作经验丰富。 <br />
<br />
&nbsp; &nbsp; ……曲啸教授悄悄跟我谈：“阎同学，我这次来其实压力很大的。我回去后必须给我的听众们一个交代。但我这么到处演讲，对美国也就是走马观花。你能不能帮我个忙？”我一听立刻明白了，便告诉他绝对没问题。他说：“那太好了，散会后你到我们的旅馆，我们俩谈谈。你把你自己看到的，听到的，美国哪些方面不如中国的例子告诉我一些。我来的时候有个想法，就是说美国的物质生活比较发达，但精神生活不如中国。但我没有例子啊。你给我讲点这方面的例子。你看行不？”&nbsp;&nbsp;<br />
<br />
&nbsp; &nbsp; “绝对没问题。你要听多少美国不如中国的例子？” <br />
<br />
&nbsp; &nbsp; “越多越好啊，要是很多，那就救了我的命了。” <br />
<br />
&nbsp; &nbsp; ……我就慢慢地谈了起来。比如，美国虽然没有世袭制度，但个别家族还是代代为官的，比如肯尼迪家族。而中国早就在毛泽东时代结束了世袭制，毛岸英即使活下来毛主席也不会把江山交给他的。我敢肯定，未来的江山决不会交给高干子弟的。这一点，你回国后可以大胆宣传美国腐朽的变相的世袭制。此时刘中海先生从厕所里出来了也参加了我们的讨论。刘中海先生说：“周总理多次讲过，我们的政权永远是工农大众的。”曲啸教授立刻让我接着讲。我说，美国虽然科学发达，可还是有矿工到地下挖煤的，你看，去年就发生了矿难，有两人被埋在里边，政府出动了力所能及的力量，连夜打洞，挖出来的俩矿工还是死了。这在中国不会发生的！过去没有过矿难的报道，现在没有矿难，将来也不会有的。曲啸教授说这个很重要，可以告诉国内。刘中海先生说：“周总理一直要求安全第一，所以不会有矿难发生。资本主义国家就是追逐利润，怎么会管矿工死活？”曲啸教授很高兴说他回国后要讲这一点。在我们社会主义国家，永远都不会有像美国这样不顾矿工死活的矿难。然后他让我继续讲。我告诉他俩最近报纸上登载了一个案子，是一个保姆遭到了雇主的性侵犯。这种事，在我党领导下根本不需要法院解决，党组织就彻底解决了。把保姆当成第二个老婆，就是变相多妻制，这在我党领导下是不可想象的，这种糜烂现象将来也不会发生。曲啸教授点头赞同，对我对未来的预测非常认同。&nbsp;&nbsp;<br />
<br />
&nbsp; &nbsp; 看看表，时间到了，我们就开车去听曲啸的演讲了。 <br />
<br />
 <br />
<br />
（二）曲啸心理崩溃的前前后后 <br />
<br />
&nbsp; &nbsp; 我开车把二位拉到会议室，令曲啸教授心理崩溃的事件就开始了。 <br />
<br />
&nbsp; &nbsp; 联谊会会长通知大家去听国内来的曲啸教授演讲，但几百人的留学生中只有二三十个人去听了。原因有两个：一个是曲啸在国内很红，但来到海外的人没几个知道他是何许人也。另一原因是大家都厌烦了几十年的政治说教，对爱国教育的说教不怎么感冒。但有两位“爱国华侨”教授参加了。这两位教授是从台湾到美国留学并留下来当教授的，由于他俩恨透了蒋介石的国民党，也就非常热爱共产党。这样，他们俩不参加台湾的联谊会，而专门参加大陆（也就是中华人民共和国）来的学生学者联谊会。由于他们俩的热情，其中一人便成了联谊会的顾问，仿照当时邓小平给胡耀邦当顾问的头衔办理的。也就是说，不论谁被选为会长，他都是深受大家尊重的长者和领导。他的名字就是大名鼎鼎的近代历史学家汪荣祖教授。&nbsp;&nbsp;<br />
<br />
&nbsp; &nbsp; 事就出在了汪荣祖教授的幡然醒悟。 <br />
<br />
&nbsp; &nbsp; 虽然大家从刘中海先生的开场白里知道了曲啸教授的名字在国内已经响彻云霄，但一开始也觉得刘中海先生在讲大话空话，一个大学教授怎么可能成为全国人民妇孺皆知的人物？估计有点忽悠。 <br />
<br />
&nbsp; &nbsp; 曲啸教授的演讲非常精彩，第一句话就说：“当年我也有跟你们一样出国留学的机会，虽然那时只能是去苏联。可是，我却被打成右派，还进了监狱。”听到这里，大家立刻聚精会神地倾听他的遭遇。曲啸先生绝对有演讲天赋，那声音时而抑，时而扬，时而顿，时而挫，时而高亢，时而柔和，时而激愤，时而婉约，时而高山流水，时而天地含悲。&nbsp;&nbsp;<br />
<br />
&nbsp; &nbsp; 比如当他讲到当时如果他有200块钱，他就可以给他心爱的女人治病，他的女人就不会离他而去的时候，演讲进入了高潮。高潮一过，他又开始讲一些小事。突然间，又有了更令听者心理震颤的故事，就是因为在劳改农场里一位当地女孩给他送大饼，他后来就娶了那女孩当老婆，也就是他演讲时依然在任上的妻子。 他爸爸让苏军卡车压死了，非但没有给一分钱赔偿，反而把他定为反革命，因为他爸爸被苏军卡车压死了，猜测他心里必然恨苏联，反苏就是反党，就是反革命，坐牢22年。&nbsp;&nbsp;<br />
<br />
&nbsp; &nbsp; 曲啸的演讲由于是亲身经历，又经过了无数次演讲锻炼早已熟记于心，几乎找不到哪怕一点点破绽和前后逻辑不符的地方，甚至连一句病句都很难找到。 <br />
<br />
&nbsp; &nbsp; 对于经历过三年大饥荒、文革动乱的我们大多数人来说，他的故事虽然震颤，但毕竟他没有我们当年亲自看到的被活活打死的地富反坏右那么悲惨，我们听起来也就是赞叹他的演讲能力，对得起他那法学家兼心理学家的头衔。 <br />
<br />
&nbsp; &nbsp; 坐在我旁边的汪荣祖教授受不了了，他几次想打断曲啸教授的演讲而提问题，可是当时不是在教室演讲，是一个小屋子，汪荣祖教授在曲啸教授的右边靠后，他没看到汪荣祖教授举手提问。汪教授也就不再提问了，也许他要问的问题曲啸教授在他后面的演讲里很快就逐一给出答案了。&nbsp;&nbsp;<br />
<br />
&nbsp; &nbsp; 当我们听到故事的最后，那就是他被胡耀邦同志平反一切冤假错案而得到昭雪了，我们以为他的演讲也就结束了，突然间，但听曲啸教授一个“但是，”才知道他的演讲还没完。他后面的演讲应该不算是演讲了，而是对年轻人的教育了。“党就是妈妈，妈妈打错了孩子，孩子是不会也不应该记仇的！”&nbsp;&nbsp;<br />
<br />
&nbsp; &nbsp; 听到他这句话，大家沉思着。也就明白了曲啸教授来美国巡回演讲的目的了。这也是他开始演讲时我本能地预料到他最后要说的话。我看了一眼刘中海先生，他此时的眼睛仔仔细细观看留学生们的表情。 <br />
<br />
&nbsp; &nbsp; 本来会议到这里也就该结束了。可突然间，汪荣祖教授说他要发言。大家对汪教授要说什么早就知道了，反正每次大家开会他都发言，告诉大家国民党蒋介石是何等独裁何等残忍。对他的发言大家也没有啥反感，早已习惯了，再说了，只要大家有难处的时候，他会尽力帮忙的。&nbsp;&nbsp;<br />
<br />
&nbsp; &nbsp; 汪荣祖教授脸色通红，跟过去判若两人。他非常震惊的内心世界在他的发言中表达得淋漓尽致。他说：“我过去只知道蒋介石国民党是如何独裁，如何玩政治，不诚实，专门欺骗台湾人说共产党毛主席是多么独裁，多么血腥，多么残酷地对待不同政见者。对国民党的宣传我从来都反着读，绝不相信国民党的骗子把戏，而真心相信大陆共产党的报纸，因为那些报道都是跟国民党的说法相反的。可是今天，曲啸教授的演讲，当真是血泪的控诉，句句血，声声泪！一个青年学者平白无故就坐牢22年！而这些，我在台湾时也看到过类似的报道，但报道的事件没有这么邪乎，没有这么真切，没有这么令人愤怒。”&nbsp;&nbsp;<br />
<br />
&nbsp; &nbsp; 听到这里，我看了看刘中海先生，他已经坐不住了，他的脸色苍白，表情显示着后悔、吃惊、恐惧与遗憾。我忍不住去看曲啸教授，他的嘴唇在颤抖，他的腿也在颤抖，突如其来的打击如同晴天霹雳打得他晕头转向。他不知道是该坐下听，还是继续站着等待问问题的讲完后给出回答。&nbsp;&nbsp;<br />
<br />
&nbsp; &nbsp; 相比之下还是刘中海先生比较镇定自若，在汪荣祖教授停下来的一刹那，便立刻站立起来，想停止汪教授的评论。可汪教授摆手给他往下压的手势，意思是他还没有讲完。他继续着他幡然悔悟的评论：“什么党是亲娘，可如此长期地打自己的孩子，那还是亲娘吗？比后娘都残忍，还有什么资格要求被虐待的孩子忠诚于她？母亲这样对待自己的孩子，在任何文明国家都是非法的，都要受到法律的制裁的。”汪教授如此愤怒令我震惊。我震惊的不仅仅是他的愤怒，也不是他的表情，而是他作为一个历史学家，怎么可能如此无知。想来想去是他的偏见造成的。当真是：偏见比无知离真理更远。但这也表明，汪荣祖教授的内心是真诚的，他是个想说实话的历史学家，而非为了利益不择手段的政客。&nbsp;&nbsp;<br />
<br />
&nbsp; &nbsp; 联谊会会长一看唯一的办法就是停止汪荣祖教授的继续发言，就在汪教授间歇的一刹那，他立刻站起，说二位刚下飞机，匆匆忙忙从国内来到美国，十分疲劳，明天他们还要赶路呢。会议到此结束。 <br />
<br />
&nbsp; &nbsp; 按照跟曲啸教授谈好了的计划，我还是去了他们的旅馆……刘中海先生把门插好，便看着我和曲啸。我知道他在想什么。曲啸的脸色还是苍白得像白纸，似乎眼球都不转了，在突如其来的打击下痛苦地挣扎着。 <br />
<br />
&nbsp; &nbsp; “阎同学，以你的看法，那位所谓的爱国华侨是真的痛恨国民党？而非潜伏到我们留学生内部拉拢同学，在关键时刻不惜暴露了身份？”刘中海先生警惕地探问汪荣祖先生的用意。我实话实说，告诉他汪教授绝对是可靠的历史学家，不是什么特务，他对大陆的不了解产生的误会被突如其来的真实报告给打醒了。&nbsp;&nbsp;<br />
<br />
&nbsp; &nbsp; 刘中海先生点头认同了我的看法，然后他说：“我认为曲啸先生的巡回演讲不能继续下去了。这个后果是没有预料到的，但可是在情理之中的。我刚才在车里想，即使在国内也未必没有人跟汪荣祖的观点一致，只是没人说出来而已。所以，曲啸教授的演讲如同一把双刃剑，到底是利大于弊还是弊大于利实难预料。你认为呢？”曲啸看着刘中海一句话也没说，没有回答他的问题。我看得出来曲啸教授内心的痛苦已经到了极点。这表明他的演讲生涯结束了。刘中海先生回去后一定会向上级如实汇报在美国发生了什么，因为终止曲啸的巡回演讲他需要跟大使馆教育处商量的。曲啸教授明白他的利用价值已经没有了。对这一点，曲啸教授刘中海先生和我三人当时的想法应该是一致的。&nbsp;&nbsp;<br />
<br />
&nbsp; &nbsp; 我知道曲啸教授此时再也没有必要让我给他讲一夜美国哪里不如中国的故事了……<br />
<br />
&nbsp; &nbsp; 我当时担心曲啸教授的心理崩溃会导致精神崩溃而使身体垮掉是有道理的，他那极端心理崩溃的眼神在苍白的脸上折射着死人般的昏暗，令你感到寒冷和哀凉。那已经不是失望，那是绝望。想必他在来美前已经计划好了回去后大展宏图，继续红遍大江南北长城内外。然后便是中央委员？由“中央宣传部调研员”升为宣传部部长？这个，可是润涛阎的话，曲啸教授可没这么说。但他在汪荣祖教授没发言前的兴高采烈和对我给他介绍“美国哪里不如中国”故事的期待，可以看出他对此次赴美巡回演讲是何等志在必得。&nbsp;&nbsp;<br />
<br />
&nbsp; &nbsp; 曲啸回国后基本上不再参加活动了，不久就大脑里得了病，1991年到南通演讲，病倒在演讲台，从此半身不遂，也失去了说话能力。一直靠他“一张大饼换来的婚姻”故事里的妻子（他讲这段故事的时候说，他跟她毫无感情基础更无共同语言）给予无微不至的照顾，十几年如一日，直到他去世。俺估计那卧床不起的十多年里，他又感受到了他们之间是有感情基础的，是有共同语言的。从他妻子的角度来讲，曲啸教授突然心理崩溃而导致大脑得了病而卧床不起，对她的婚姻来说也许是好事一桩呢。&nbsp;&nbsp;<br />
<br />
 <br />
<br />
（三）后记 <br />
<br />
&nbsp; &nbsp; 希望大家转贴这篇短文，故事里的主人公只有曲啸教授不在世了，而刘中海先生和汪荣祖教授都在世。希望他们能看到此文，尤其是刘中海先生，我离开旅馆后曲啸教授是如何度过那段时光的？彻夜未眠？你们二人是立刻回国了？还是在美国旅游了一段时间散散心？曲啸教授在美国是否已经由心理崩溃转成精神崩溃了？还是回国后经历了被冷落的打击才大脑出了毛病？希望刘中海先生能补充一下后面的故事，因为大家都不知道曲啸教授那么健康突然就大脑出了毛病，最后导致半身不遂。毕竟曲啸教授成了那一代知识分子的三个代表，而他早期的经历已经由电影《牧马人》详细介绍了，后边的故事我只能写这么多，剩下的由刘中海先生补充，便是对当年“全国青年人的良师益友”、“共和国三大演说家之一”曲啸教授的崇拜者们的一个交代，也解答了他们对“曲啸热”突然冷却下来了的历史疑问。&nbsp;&nbsp;<br />
<br />
&nbsp; &nbsp; 曲啸教授的故事毕竟拍了电影，平心而论他的经历和演讲能力比李燕杰毫不逊色，但李燕杰就比他幸运多了。下面看看国内网站的名人介绍，李燕杰走遍世界都没有遭遇到一次曲啸经历过的尴尬局面。文章对曲啸也曾到过美国做演讲的历史只字未提，表明国内很少有人知道他曾来到美国。难道曲啸教授就是悲剧的命运？他干嘛要碰上爱国华侨汪荣祖教授？&nbsp;&nbsp;<br />
<br />
&nbsp; &nbsp; 我跟曲啸教授只有几个小时的接触，但无论如何也认为他是一个诚实的人，一个历经磨难的人，一个不抱怨只感恩的人，一个性格柔软到没有骨头的人，一个可以利用的人，一个可以交谈的人，一个可以交往的人，一个吃了我的水饺后说润涛阎做的水饺好吃的人。同样，汪荣祖教授也是一个诚实的人，一个爱憎分明的人。但作为著作等身的历史学家竟然如此天真，确实有点不可思议。 ...</div>

							
							
							
							
