													<h2>曲啸事件的政治心理学分析</h2>
						
						
																											<div id="postmessage_618028" class="t_msgfont">『关天茶舍』 曲啸事件的政治心理学分析 <br />
<br />
<br />
 作者：邝海炎 提交日期：2010-5-6 19:05:00 访问：4008 回复：126&nbsp;&nbsp;<br />
<br />
&nbsp; &nbsp; ?? 润涛阎先生这篇回忆文章确定相当经典，是上等的政治学案例。试分析如下：<br />
　　1，曲啸事件宣告了“戈培尔效应”的死穴。“戈培尔效应a”认为：重复是一种力量，谎言重复一百次就会成为真理。但这种效应只有在一个封闭的权力系统中才成立，如果面对一个异样的权力系统，再怎么重复也不会成为真理。“戈培尔效应b”认为：宣传不能是纯粹的谎言，必须在谎言中掺杂一定的真理，这样才有鼓动性。可这一定的真理却会对谎言的结构进行腐蚀和破坏，曲啸举自己的受难宣传“孩子不能怪父母”，却被汪荣祖责难：“天下没有这样的父母”，这种尴尬是“真理”本能的成功表现。<br />
　　　　2，曲啸在文革后还为党国做喉舌，唯一的心理基础就是“以前是父母打错了孩子”，这样画圈圈保持国家与个人的“拟父子关系”，在80年代的中国确实具有普遍性，号称中国良心的巴金其实也是这样。对于巴金，人们已经说的够多了，尤其是他提倡的“讲真话”。但巴金“反省”的成绩到底如何呢？张志扬注意到“‘痛苦’与‘死亡’是推动巴金“讲真话”地两个主动轮。接下来他便通过跟踪《随想录》地文本，理出了清单一：<br />
　　　　巴金‘讲真话’的主要内容<br />
　　1）控诉‘四人帮’、‘造反派’、或‘文革派’的罪行；<br />
　　2）袒露自己的‘痛苦’何‘怯弱’；<br />
　　3）忏悔自己‘投掷石块’的丑恶；<br />
　　4）探寻‘说假话的根源’；<br />
　　5）重申知识分子‘独立思考’的职能；<br />
　　6）呼吁建立‘文革博物馆’、建立心灵不让文革重演的警世碑；<br />
　　7）而且碑文要刻着：‘没有神、也没有兽、大家都是人。’<br />
　　　　当然，这七个方面并不是从一开始（1977年）就作为明确意向而平列的，所以，它有一个逐渐深入、或逐渐显豁的过程，反映了巴金“讲真话”的“真”的思路于层次，因此这需要有更真实（现象学的真实）的清单二：<br />
　　　　巴金“讲真话”“真”的思路与层次<br />
　　A、控诉‘四人帮’，一切罪恶归‘四人帮’。<br />
　　B、批评‘长官意志’，要求‘民主法制’。<br />
　　C、反省‘意识形态’，申张‘独立思考’。<br />
　　D、消除‘领袖崇拜’，恢复人的自由。<br />
　　　　可以说，“临摹”到了这里，跟踪也就结束了。也就是说，前两个问题已经回答，接下来就是更“要命”的“问题3”了，结果是似乎是这样――<br />
　　　　学生：巴金　　问题：究竟是什么“迷魂汤”使“造反派”变成“食肉寝皮的虎狼”，同时又使“知识分子”心甘情愿地变成“任人宰割地牛”？　　回答：归根到底使批者“革命”外衣的封建主义狂热使然。<br />
　　　　张志扬是位细心的“老师”，他循循善诱，写下了一个让人心服口服的批阅―― 　　巴金一生只说了一句话：“封建吃人”。但只在“始”与“终”。（中间是“异化”的）　　巴金之“始”说的“封建吃人”是对“旧社会”的控诉，而巴金之“终”说的“封建吃人”是对“新社会”的揭露。而这“新社会”显然指的是MC（毛泽东时代的中国）、而非DC（邓小平时期的中国）。<br />
　　　　当然， 张志扬绝不是一个普通的“老师”，他始终有自己的“问题意识”，所以他接下来问：“ 从切身性的创伤感看，60、70年代年代的强度不知要超出20、30年代多少倍，但就是没有把新的记忆打出旧的记忆模式而获得自己的命名，结果还是套在旧的记忆模式中，不过加了一件‘革命的外衣’，‘封建主义’的胃口依然那样贪婪地吃人”，这是为什么？<br />
　　　　是啊，这是为什么！为什么巴金的“个体立场”不能坚持到底，为什么巴金的反省不能冲破作为“类”的“意识形态”的樊篱，完整的获得他自己？<br />
　　　　张志扬最后写道，“除了被损害的痛苦和修辞学的呻吟，巴金的‘武器库’中没有触‘类’旁通的武器”――<br />
　　　　“巴金的经验与思想只限于类比之中，用他感受过、理解过的‘封建主义’记忆来套眼前经历的‘新社会’中独断专横的种种现象。或许巴金的喊叫能惊醒人们思考，但它还不是思考本身。”（见拙文《巴金：为什么在反省中画圈圈？――读张志扬《创伤记忆》（二）》）<br />
　　　　也就是说，曲啸的“父母打错了孩子论”其实是一种创伤记忆的在作怪而已，要减轻这种“创伤记忆”对思想的干扰，唯一的办法就是打开历史真相，让真相和阳光消淤化肿。<br />
　　　　3，汪荣祖因反国民党而亲共，其天真是积习难改的，例子有：他与李敖前几年合著的《蒋介石评传》居然还坚持中山舰事件是蒋所为，完全无视杨天石先生的研究成果；而在丢失东北上，也将责任完全归咎于蒋，为张学良卸责，不想张学良晚年却承认了是他自己下命令不抵抗的。这种毛病在李敖身上表现的更突出，李敖当年为了“黑蒋”，引用中共在香港的地下党作家唐人的演义小说《金陵春梦》中的“郑三发子”情节津津乐道于蒋介石的“拖油瓶丑事”，并以此论证蒋介石“没有担当”。等到浙江省政协发表出版关于蒋介石家世历史的调查报告和有关史料的《浙江文史资料选辑》总23、38辑后，却没见他对这个问题有什么新的“研究发现”问世了。汪、李的这种毛病其实也是一种创伤记忆使然，只不过其方式不是画圈圈，而是合并同类项-“敌人的敌人就是我的朋友”。<br />
　　　　4，曲啸的崩溃还让我想起托马斯&#8226;曼的愤怒―― ?? “1986年米奇尼克经历着人生最黯淡的时期。那是他于1989年12月之后第二次坐牢，被判三年，时年40岁。在这种时刻，他在牢里想起了一个标准的德国贵族――托马斯&#8226;曼。曼（l875――1955），德国小说家和散文家，1929年诺贝尔文学奖获得者。1933年希特勒上台时，曼正好在瑞士逗留，从此开始一去不复返的流亡生涯，1944年加入美国国籍。根据曼写于1933－1936年间的私人书信集，米奇尼克写成了这篇《堂&#8226;吉珂德和开骂》。”[5]<br />
　　　　这篇文章主要就是分析了小说家托马斯&#8226;曼由一个对“政治冷漠的人”在极权现实的紧逼下通过“开骂”与纳粹“摊牌”的过程。这样的文章内容很容易使人将目光聚焦在“作家反纳粹”这一点上，但米奇尼克却有他自己的想法，“我把托马斯&#8226;曼的一生称作――像他的信件所记录的――一部‘教育小说’。我想从这部小说里学到什么呢？重组这些章节，我并不想讲述作者对纳粹的态度――这个吸引人的题目我打算下次再谈。我只对一个问题感兴趣。我想要弄明白那种支配他作出一生中最为关键的主导性感觉。我想我做到了，那种感觉就是反感”[6]。<br />
　　　　我估计，米奇尼克这样做是基于他的如下信念：首先，“曼对纳粹的态度充满了本能的反感”，这是因为“极权主义是最为敌视作家和艺术家的，在极权主义的土壤中，最不适宜真理和艺术的生长”[7]。对此，崔卫平先生是这样解释的，“最富有创造性的艺术创作，也必须借助人类生活的一般背景和一般价值。他可以对这些背景和价值作出自己的阐释和改动式的评价，但是不存在这么一个地平线，创造的活动就失去了缘由。野蛮的纳粹是对于这些一般背景和价值的疯狂破坏，它所造成的结果是，不仅一个作家借以观察世界的基本光线不再存在，而是连基本的人类生活都不复存在，剩下来的只有废墟和荒芜。这完全构成了对于一个作家基本工作条件的破坏，构成了他作为一个人在这个世界上所需要的起码生存条件的破坏。这时候如果继续对野蛮保持沉默，无异于认同一场对于自身的攻击，更何况这种野蛮事实上已经造成对于某些人们的实际攻击，那些素不相识又是休戚相关的受害者。”[5] <br />
　　　　（见拙文：《从米奇尼克出发》） <br />
　　　　――与“反感”不一样，曲啸事件我想提想起一个概念――“恶心”。恶心是什么？就好象一些神怪电影常有的镜头：有的人过着锦衣玉食的生活，突然头一天，那些锦衣玉食现出原形，原来都是蛆虫、蝎子，蚯蚓变的，他受不了，只有呕吐。所以，政治伦理学上“恶心”就是突然发现自己秉承的价值观是如此的丑陋，如此非人，如此不可忍受！<br />
　　　　“恶心”为什么会发生？首先，只有把自己当人，而不是国家机器的螺丝钉，“恶心”才会产生。如果说，“反感”是自然人性对极权的鄙夷，而“恶心”则是受害者恢复自然人性后的不适应。毛时代是极权时代，其特点就是：无论在任何方面，你必须与我保持一致。在那时，人只有国家机器上的一颗螺丝钉，自然不存在人的“恶心感”。而邓时代是威权时代，其特点是：只要你不反对我，就允许你存在。所以，威权时代的“政治人”属性很难存在，但经济人、社会人、甚至娱乐人属性还是存在的，这些人性的存在使我们恢复人的局部“视力”，一旦与异质权力系统对比，原有价值的“恶心感”就油然而生。<br />
　　　　其次，“恶心”是需要对比的，而对比的前提就是双方承认可以对比。在毛时代，任何与国家权力系统异质的价值观都是不允许的，美国人不是人，是美国鬼子，所以，人与鬼子不存在对比，当然就不会产生“恶心”，反而像蛆虫变的美食一样，能迷惑何炳棣这样优秀的历史学家。而到了邓时代呢，我们开始承认不管美国人、中国人，都是人了，这样就产生了对比的可能。所以，“恶心”是政治人的正常价值反应，是人性回归的表现。中国有句俗话，“压跨骆驼的最后一根稻草”，“恶心”就是这样一种“致命”的情绪。</div>

							
							
							
							
