朝鲜战争爆发的原因，是金日成想统一朝鲜，而美国的战略规划中，只打算确保日本，明确的不打算协防韩国――这是金日成发动战争的最根本原因。<br />
所以，如果说朝鲜战争有机会避免，那这个机会就是美国应该明确他会协防韩国。<br />
只要明确这一点，金日成就绝不会冒与美国开战的风险进攻韩国。<br />
<br />
二、中国是否应该参战？<br />
当美军仓促加入朝鲜战争，并且在仁川登陆彻底击败北朝鲜军队后，中国是否参战正式成为了一个问题。<br />
要回答这个问题，只需要反问一个问题<br />
如果，中国不出兵会发生什么？<br />
<br />
&nbsp;&nbsp;1&gt;没人知道联军是否进攻中国，如果进攻中国，毫无疑问中国必须提前参战！<br />
&nbsp;&nbsp;2&gt;如果联军不打算立刻进攻中国，那么会发生生？<br />
<br />
中国边境将出现一个统一的韩国，和几十万大军压境的局面，美军随时可以从朝鲜半岛袭击中国的工业基地东北。<br />
<br />
中华民国作为美国的盟国，很有可能会将军队部署到韩国，这样国军就可以随时从辽宁和福建两个方向反攻大陆。<br />
<br />
中朝边界的丹东距离沈阳仅241公里，鸭绿江大桥距离沈阳仅392公里<br />
以美军的空中优势和战斗能力，只要3个师，5天之内就能占领沈阳。封锁整个东北，把辽沈战役重演一次，只不过失败者变成共军。<br />
<br />
面对随时可能发生的局面，中华人民共和国唯有在以后几十年内随时戒备。<br />
这样的成本绝对是耗不起的。<br />
<br />
所以说，如果有什么可能让中国不参战：<br />
1.我党没有取得中国政权<br />
2.美国不越过38线<br />
3.美国声明绝不会进攻中国（即便有这条，我党还不一定相信）<br />
<br />
<br />
三、中国胜了吗？美国败了吗？<br />
看看最后的军事分界线就清楚了<br />
答案很简单，双方打平了<br />
<br />
<br />
四、中国参战划算吗？<br />
中国参加朝鲜战争，最大的收获有两条：<br />
1. 让全世界意识到中国是一个强国，尤其是军事强国。<br />
他能独立与美国抗衡，并且不落下风，以后所有国家与中国有军事对抗可能时，都要仔细考虑朝鲜战争；后来一连串的战争更加证明了中国在东亚的军事强国地位<br />
2. 在东亚建立了自己的缓冲区<br />
3. 中国军人伤亡惨重，付出代价很大<br />
4. 丧失了收复台湾的机会（不参战也丧失此机会了，因为朝鲜战争一爆发美军就决定协防台湾）<br />
<br />
从国家战略的维度来看，这样惨重的牺牲无疑是难得的，毕竟中国在抗日战争付出几千万人的代价，也没有换来一个货真价实的世界地位，但是通过朝鲜战争获得了。<br />
<br />
