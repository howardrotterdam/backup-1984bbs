													<h2>差距!!令人不安的事实!（转）</h2>
						
						
																											<div id="postmessage_387816" class="t_msgfont">作者：摩诃萨 <br />
<br />
这些年来,我往返新加坡和上海数次了,每每看见那拔地而起的高楼大厦,每每看见那眼花缭乱琳琅满目的商品,我从没有显得高兴,或者感受到自己祖国的富强.首先我要说，中国的制造业不发达。我是学机械制造的，说说我个人的一些看法:当我熟练地操控着庞大的五轴数控加工中心机床,当我看着主轴灵巧而准确地按照我编写的程序360度地旋转着,进刀退刀时,当我做出了精确度达到0.001毫米要求的样品时，我兴奋了一阵子，但紧接着又有了失落感，因为那台加工中心的数控机床是德国造的，数控计算机关键部分是德国造的，加工软件还是是德国造的，就连一把车刀，一个钻头，都是日本造的！没有一样工具是中国制造,连最基本的扳手也出自日本,我的心情是失落的..... <br />
<br />
　 同胞们，你们不是搞这个的也许没有深刻的体会，我要加工一件精度为0.001毫米的零件，所需的工具，就是上述外国制造的东西，这些工具的精度要比产品的精度高好几倍甚至几十倍几百倍！一把用于高精度加工的小小的钻头，咱们就是做不到啊！买日本的只要几十元啊！(当有一天,我们全部安心于买,而不自己去尝试制造时,而别人不卖我们的时候,想一想,我们会怎么样呢)不要以为会一些 photoshop之类的时尚软件就沾沾自喜，没有看见过会透气的铁吧，没有见识过10秒内直接打穿50MM厚的不锈钢而直径只有8MM的钻头吧,什么是差距？这就是差距啊,如此令人不安的差距啊!<br />
<br />
　这些年来,我们时常会看见一些国家新推出的机器人的新闻，当电视播放日本大学生制造了一个机器人拿到德国与德国大学生的机器人比试时，我是真的傻了眼，那个机器人就是可以双脚保持平衡自我行走的那种，不过没有外壳，稍微懂点这行的我一眼就看出它身上布满的控制器、马达、线路，镜头一闪而过，更多的是机器人不靠任何帮助自行双手翻跟头，90度鞠躬，双脚蹦跳前进，我想你们都露出笑容&quot;真好玩，太可爱了&quot;，而我除了惊讶，还有发自内心的．．．害怕！真正的害怕，这个差距太大了，这就是制造业，自动控制，材料学的成就，我恨日本，所以我才害怕。<br />
<br />
　　外行人不懂这个差距有多大，我这样说，如果战事爆发，中国的制造业还与外国有如此差距，你，还有我，就只有小米加步枪跟人家打了，这不是危言耸听啊!我们始终沉浸在国民生产总值有多少万亿,我们在国际上排到了第几位,有没有去想想这些国民生产总值里有多少的质和量....再回头看看周围,有多少年青一代的人在讨论制造技术还是在讨论股票?是想好好地提高自己技术和人文修养,还是在梦想一觉睡醒是千百万的富豪.再抬头看看头上那片天,曾经很蓝的一片天,还找得到那曾经的蓝吗?<br />
<br />
　　现在社会好像越来越偏重对国计民生不起关键作用的产业了，什么房地产开发，计算机，软件，商业，搞得好也行，少数人把房子炒得越来越贵，这种发财之道没什么值得骄傲的,我跟你说:说白了就是缺德！计算机普及了，核心制造技术我们又会多少？有几块芯片我们能够设计和制造?软件更甭提，黑客不少(只知道去偷盗游戏的帐号)，可人家软件又好又便宜，你怎么竞争？搞电脑的人太多了！不要以为科幻片都是真的，实际中电脑不会打仗，真正接触的是机器！电脑差距还不是很大，打起仗来，当导弹不准，子弹卡壳，坦克熄火的时候，你还有我还有大家，就真得拿着大刀往前冲了....我已经很少看见我学生时期的那种水准的技师了,也看不见他们的传人在哪里....只看见网吧里热火朝天的埋头于虚拟世界的年轻人,只看见满大街的爱得难分难舍的年青人,只看见那些一周只上几节课的大学生......<br />
<br />
　　不要看到靓车就发晕，先不谈２０出头的女孩为国家创造了什么生产价值才能换来改装悍马越野车，要是查老底，中国有钱人有几个原始积累阶段不是靠违反规则的手段来的，敢说不违背良心的又有几个？<br />
<br />
　 越有钱和能力就越有责任为国家为人民出力，你再看中国现状，有多少有钱宁可买日本十年前的技术生产的轿车在同胞面前炫耀也不把钱投入社会，来之于民归之于民？每当我看到外国车时，我的心里是别扭的，因为我是搞制造的，可我知道咱们祖国连汽车外壳的曲面精加工都困难更甭提发动机了。快点懂事吧，醒醒吧，别再沉迷于ＧＤＰ又增长了，你看看咱们制造的产品的质量，心痛啊！别再像不懂事的孩子似的围着跑车看个不停，拿着超薄随身听等外国造的数码产品到处炫耀了，那是人家淘汰的技术.一个国家的支柱，脊梁，是制造业！<br />
<br />
　　过去２００年，未来我不知道，反正这个现状是不会变的，别以为能赚钱就是本事，就值得骄傲，当有人炫耀知道见过多少名车，卖出多少房产，设计多少网页，得到谁谁谁的签名的时候，我承认那是你的劳动成果，但我会不屑一顾，我衡量人的能力只看它是不是真的为社会为国家创造出实际的价值，是不是真的提高了生产力，虽然我整天和冰冷的机器打交道，操作复杂的零件三维设计软件熬夜画图，穿着老土，跟不上流行，不知道现在谁最火爆，但面对别人询问我的情况时，我会毫不犹豫的全盘托出，才不管他怎么瞧不起我，因为我是搞机械制造的，作为一个劳动者，一个制造业工作者，看见那些离开了制造业的人们(我以前的师兄弟们,都已经不做这一行),我好心痛,新年归来,每每念及此事,心如刀绞,辗转难眠......</div>

							
							
							
							
