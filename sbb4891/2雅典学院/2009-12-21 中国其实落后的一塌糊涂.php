													<h2>中国其实落后的一塌糊涂</h2>
						
						
																											<div id="postmessage_386274" class="t_msgfont">某次闲谈，一位在国防系统工作了40年的父辈人物对我讲到，中国制造原子弹的技术，其实是法国提供的，我惊愕之余，细细想来是很有道理的。<br />
<br />
原子弹的技术，现在人说起来，好像并不困难，甚至有些西方的技术发烧友，也可以整上一颗来玩玩。这只不过反映了人们对技术喜新厌旧的一种心态，有这么多国家掌握了这个技术，那自然原子弹就是个小儿科了。<br />
<br />
事实当然并不简单，比如通过解密的资料，现在已经可以证实，当年苏联能够成功的试爆原子弹，是窃取了美国的技术。以苏联技术力量之强，尚不能凭自己的力量搞原子弹，更何况用土炉子炼钢铁的中国。这不是看不起自己的祖国，而是科学技术是需要大量积淀的复杂系统工程，不是靠喊口号、打冲锋就可以轻便解决的。比如伊拉克和伊朗，说起来都是力量不俗的国家，因为有石油，也算富甲一方，照样搞不出原子弹。可见，完成一个复杂的科学技术成果，需要相应水平的社会组织能力，在这方面上，中国未见得比伊拉克和伊朗好到哪里去。<br />
<br />
说到这里，有朋友会问，法国为何有原子弹技术，她又缘何愿意给中国？首先，法国、日本、德国、英国虽然小一些，但是社会组织管理的水平很高，科学技术能力超群，所以研究出原子弹都不存在问题，日本、德国只不过是不想做罢了。我有一个观点，今天的中国人反日呼声很高，其实现在让中日再战一场，世界上的其他国家都两不相帮，中国一样还打不过日本。因为日本有自己全套的工业研发制造体系，包括软件、元器件、航天、航海、机械、基础科学等，早在60多年前，就造出N多的航空母舰和美国大打出手，日本的国力和工业水平是中国完全不能望其项背的。<br />
<br />
法国为什么会给中国原子弹，是因为她一直是西欧的一个异类，居然退出了北约组织，这样法国就成为了既不属于苏东、又不属于美国集团的孤立国家。法国要寻求自己的政治盟友，自然和同样与美苏都闹翻了的中国是天然的伙伴，从政治上来说，要结交中国，原子弹技术就是最好的礼物了。<br />
<br />
作为普通人，大家往往有的一种想法，总觉得承认西方的科学技术比我们发达，就意味着承认西方人比中国人聪明，所以大家都憋着要去摘科学王冠上的明珠，其实科学是不能做这样个体的比较的。科技的发达，社会背景资源的决定性作用，这里面包括科研探讨环境、管理机构水平、资金条件情况，这些要素的综合作用，要远大于单纯个人的努力，这就是唯物主义，就如同站在不同高度的台阶上互相比个子，如果台阶的高度相差过大，则主观能动性的发挥基本上已经不可能改变比较的结果了。所以，当有人宣称他的数学成果是500年来第一人的时候，笔者作为一个中学时曾梦想着参加奥林匹克数学竞赛的爱好者、理工科大学毕业生，我甚至都无须去了解他的科研成果，就可以判断其错伪了，因为中国的整体科技水平的低下，决定了作为其中一个单一学科的单一分子，根本就不可能有过分突出的个体表现，背景限制了你可以获得成就的可能性，而非中国人不聪明。就如同美国人、欧洲人再聪明，也不可能取得围棋和中国象棋世界冠军，因为他们没有适当的土壤和培养环境，倒不是说外国人没有中国人聪明；而相类似的中国人在国际象棋上的水平不够，也一样不能说明中国人比西方人笨。可是中国人却总在邪门歪道上想办法，比如国际象棋中国人下不过西方人（日本人的水平好像也不怎么样），本是先天决定的事情，搞几个女棋手出来，能证明什么，中国的男人都不行？国人确实喜欢放卫星，欣赏病梅式的美丽，这不是和大跃进时候亩产万斤一样不符合客观规律吗？违反客观规律到底好不好呢?这恐怕是不以人的主观意愿为转移的。<br />
<br />
类似的事情还有很多。例如，一次偶然碰到一哥们，是曙光计算机研发部的，就是开发大型计算机的，我本身是学理工科的，所以关心科技多一点，便好奇的询问，大型计算机的研发是怎么做的，他们上班都做什么，用什么工具？哥们答道，我们上班都玩游戏，我听后不禁愕然。哥们接着说，到年底的时候，我们只要编个程序自我检测一下，写个报告说大型机的运算次数又提高了若干亿次就OK了，反正别人也不懂，没人查。<br />
<br />
不知道大家对大型计算机的重要性有没有认识？在现代战争中，大型机是整个战场的核心，比如在海湾战争中，远在美国本土的大型计算中心是战场上所有飞机、导弹、军舰、武器的总指挥，大型机计算能力（即计算次数）的强弱，直接决定了一个国家武器水平的高低，而一个国家的军事水平在很大程度上是由武器水平决定的。在抗日战争期间，日本只派了一个机械化师团来中国参战，这个师总共击败了中国70多个师的部队，真正是狼入羊群，不能说中国人都不勇敢，双方的装备水平差距造成了这样残酷的事实，而后来日本的机械化部队全部被美国消灭了。<br />
<br />
我们国家文革时代的科研水平也很落后，但那个时代好歹还比较老实，承认自己的不足，现在的牛皮往往吹的大，其实原创性的东西可能更少。所以我看见所谓动听的技术名词如龙芯或者先进的飞机、战船者，内心早已如对曙光计算机，失去了认同感，知道是纸糊的灯笼，破碎只是迟早的事情，有幸能瞒过去，只是没有现场检验的机会罢了。<br />
<br />
中国改革开放这么多年，并不是在所有方面都不参与世界的检验行为，真刀真枪的去参加竞争。这方面的成绩，表现比较实在和突出的是在体育方面，因为体育竞争公平公正、目见心明，胜负间缺少了许多语焉不详的托辞与暗箱操作的机妙。中国的体育项目往往是单人的项目成绩还比较出色，当然主要是依靠了我们运动员超人的吃苦耐劳精神，但凡人数超过了2人以上的体育项目，中国队则无一不是一塌糊涂。这其中比较突出的项目就是足球，中国足球经历了多年的大溃败以后，现在早已沦为亚洲的二流球队，全无往日志向，算是彻底打回原形了。相比较起中国在亚洲的田径项目中也是绝对的老大，中国运动员的身体素质条件在亚洲起码是一流的，因此没有任何理由来解释足球在亚洲比赛中成绩的没落。这体现了我们文化与社会组织管理水平的空前落后，而正因为此，我们基本上无法进行复杂系统的项目运作。所以和很多人意淫的中国强国地位相比，我们实在是落后的令人发指，只是给外国人打打小工，擅长做菜、缝衣服这些简单的工种，挣的是低端的辛苦钱。可笑的是，有些人只不过能够消费的起几件外国的奢侈品，就骄傲的觉得自己要飞天了。</div>

							
							
							
							
