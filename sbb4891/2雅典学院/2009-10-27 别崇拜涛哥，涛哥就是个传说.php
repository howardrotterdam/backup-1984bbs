																											<div id="postmessage_301998" class="t_msgfont">别崇拜涛哥，涛哥就是个传说<br />
<a href="http://blog.wenxuecity.com/blogview.php?date=200909&amp;postID=20208" target="_blank">http://blog.wenxuecity.com/blogv ... 09&amp;postID=20208</a> <br />
