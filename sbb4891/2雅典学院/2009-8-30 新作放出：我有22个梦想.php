高等教育：我有22个梦想<br />
<br />
<br />
建国初期，中国的高等教育规模尚小。据第一次高等教育会议统计，全国高校约200余所。至于培养人数，我没有查到准确资料，但可大致推算。1952年，因为“按照原有工科院校的招生规模，第一个五年计划期间只能向国家输送4-5万名毕业生，不足当时工业建设所需人才的25%”，教育部遂于1月3日颁布《关于理、工学院三年级学生提前毕业问题的几点指示》，让理工科学生提前毕业，以满足建设需求。在建国初期，理工科专业的招生规模远超文科，由此可推出当时中国高校每年的毕业人数，只有数万。<br />
<br />
60年过去，中国已拥有目前世界上乃至整个人类史上最大的高等教育规模。截止2009年6月，中国已有2317所大学（包括1983所普通高校，334所民办高校）。而据人力资源和社会保障部消息，2009年毕业的高校生总数约611万。<br />
<br />
然而，我们在拥有人类史上最大的高教规模的同时，或许也拥有人类史上最烂的高教质量。若要细数当今中国高校的N宗罪，怕用光了蜈蚣的脚趾头也不够。换种心态，我们不妨向前看，再过60年，希望中国高等教育会是怎样？<br />
<br />
<br />
1、&nbsp; &nbsp; &nbsp; &nbsp; 我希望中国高校拥有真正完善的教育方式及课程设计。60年后，学生不止是为职业而学，更为生活而学，为培养全面的理性能力而学，最终拥有如涂尔干所说的三重修养：语言的修养、科学的修养、历史的修养，而我还想画蛇添足加上第四重修养，那就是理想主义的修养。如此，大学制造的不再是流水线上的产品乃至废品，而是个性鲜明的完整的人――他们既拥有向外延伸的公共人格，关注现实，批判现实，勇敢地参与现实；又拥有内敛的私人品格，有爱的能力，同情的能力，沟通的能力，创造的能力，会肆无忌惮地笑也能够放声痛哭。<br />
<br />
2、&nbsp; &nbsp; &nbsp; &nbsp; 我希望高校里的师生关系能真正和谐。和谐建立在平等对话之上，而非其它。不和谐的师生关系主要有两种：老师因为学生出了高额学费就把他当大爷供着，哄着；学生因为封建等级残余而把老师当大爷怕着，拍着。彼此之间的关系虚伪而冷漠。我心中的理想师生关系，首先是一种平等关系，其次是一种对话关系。当然，由于老师天然的知识或经验优势，对话中老师仍应成为主导方，否则就是渎职。<br />
<br />
3、&nbsp; &nbsp; &nbsp; &nbsp; 我希望大学的教材及教学能与时俱进，譬如《马克思哲学原理》，譬如《毛泽东思想概论》，譬如《邓小平理论概论》，譬如《三个代表思想概论》。别再把长白胡子的，发霉的，乃至涉嫌洗脑的东西拿出来了，今日的学生不是红旗下的蛋，他们处在信息自由流通的多元社会，天然具备反洗脑的抗体。<br />
<br />
4、&nbsp; &nbsp; &nbsp; &nbsp; 我希望图书馆能够成为真正的学术营养餐厅。图书馆的员工不要老是一副打麻将输得屁股朝天的晦气表情，他们应当提供更亲和、专业的服务。图书馆每年进的新书也不要烂得太离谱，上架的速度也不要慢得像塞车，应该快得像赛车才对。图书馆的馆长也不当是官僚，而是学问家，就像旧时那些杰出的图书馆长――蔡元培、梁启超、胡适、陈垣、柳怡徵……<br />
<br />
5、&nbsp; &nbsp; &nbsp; &nbsp; 我希望四级考试这个臭名昭著的新八股被彻底废除。掌握一门外语非常重要，但不应该靠四级与学位或毕业证挂钩这种愚蠢的方式。<br />
<br />
6、&nbsp; &nbsp; &nbsp; &nbsp; 我希望研究生不再有发表论文的数量指标。学问不是种地，不能用亩产多少斤来度量。而CSSCI收录期刊，正是在这数量指标的荫庇下，利用版面猛力寻租。据说，目前在读的所有研究生，若都完成论文指标，以目前期刊的容量得排队到2020年。那时候火星叔叔都降临地球了。<br />
<br />
7、&nbsp; &nbsp; &nbsp; &nbsp; 我希望论文抄袭、科研造假等学术腐败能大幅度降减。这只能依靠学术体制改革，而不能指望什么“以德治学”――它跟“以德治国”在本质上都是反现代文明的。学术体制改革，实际上涉及到更深层次的改革。如前耶鲁大学校长施密德特所言：“经验告诉我们，如果政权是腐败的，那么政府部门、社会机构乃至学术机构，同样会骇人听闻的腐败”。<br />
<br />
8、&nbsp; &nbsp; &nbsp; &nbsp; 我希望制定校规的教务长们的脑壳，不再是花岗岩做成的。女学生穿的裙子有多长，情侣能否在校园内牵手，不当是校规的管理范畴，除非我们还活在班固的妹子写《女诫》的时代。这里顺便讲个段子：某大学严令禁止学生谈恋爱。一晚，教导处长巡视操场，发现一对学生恋人正在热吻，教导处长立即冲他俩大喝一声：“住嘴”！<br />
<br />
9、&nbsp; &nbsp; &nbsp; &nbsp; 我希望中国高校的后勤集团，不再是盛产千万富翁的集中营。他们喝醉了酒以后，也不要在学校里开车撞倒学生。<br />
<br />
10、&nbsp; &nbsp; &nbsp; &nbsp; 我希望中国的教授们，不再因为诱奸女学生而被捅死在课堂上，也不再将与学生的性交易视作理所当然的潜规则。<br />
<br />
11、&nbsp; &nbsp; &nbsp; &nbsp; 我希望学阀、学棍与学混都灭种。学生的创造力不该只用于咀嚼从导师口中吐出的甘蔗渣；学生的论文题目也最好不要出自导师的小抽屉。在导师过生日、生小孩、搬新房，甚至结第N次婚的时候，学生也不用像参拜教主一样去捧场。<br />
<br />
12、&nbsp; &nbsp; &nbsp; &nbsp; 我希望热捧高考状元成为一种耻辱。<br />
<br />
13、&nbsp; &nbsp; &nbsp; &nbsp; 我希望教育公平能够普照赤县神州。北京的孩子不能只因为生在伟大的天安门附近，就享有比其它地方的孩子低100分也能进同一所大学的特权。<br />
<br />
14、&nbsp; &nbsp; &nbsp; &nbsp; 我希望高校奖助学金不再是美丽的肥皂泡，那些边远地区里有天分的孩子，能够享受到接受优质教育的权利。我们这个高税负、低福利国家，应该向贫困家庭、尤其是多年来被盘剥、侮辱与损害的农村贫困家庭反哺。<br />
<br />
15、&nbsp; &nbsp; &nbsp; &nbsp; 我希望大学规模不要再盲目扩大。大学之大，不在有大把的饲养员，毕竟它不是养猪场；大学之大，也不在有大把的楼房，毕竟它不是房产企业。大学之大，在有大师，有大图书馆，有自由学术的大气象，有自由对话的大空间，还有它所培育的一代又一代的大人才。<br />
<br />
16、&nbsp; &nbsp; &nbsp; &nbsp; 我希望职业技术学校不要再大跃进成高等学校。高等教育的归高等教育，技能教育的归技能教育。欧美发达国家中，有三分之二的高中生会接受职业技能教育，而非要么挤进高等教育，要么辍学打工。<br />
<br />
17、&nbsp; &nbsp; &nbsp; &nbsp; 我希望官本位在大学里被彻底抛弃。中国的大学校长多是副省级或厅级的行政级别，这本身就是一种蒙昧与丑闻。<br />
<br />
18、&nbsp; &nbsp; &nbsp; &nbsp; 我希望高等院校发展的不公平结构能得到改善。中国从1996年开始执行“211工程”，要将全国100所重点大学建设为“国际一流大学”，第一期计划投入高达186亿元人民币，为历来最大的一笔教育投入。这种政策倾斜造成重点大学与普通大学的两级分化，与此同时，地方普通大学对学费的依赖程度越来越高――据近年一项统计，中央所属大学平均为19％，地方政府所属大学平均为40%，有的甚至高达80%。大学过于依赖学费，正是学费逐年激增的一个重要原因。<br />
<br />
19、&nbsp; &nbsp; &nbsp; &nbsp; 我希望中国高校的校友会不要开成官员会或老板会。急功近利的情感投资换不来多少捐款，真正的情感纽带建立于校友学习期间在母校的受益及工作后对母校的认同之上。截止2007年，哈佛大学获得的捐赠资金高达349亿美元。他们可不是单单靠举办几次猥琐势利的校友会。<br />
<br />
20、&nbsp; &nbsp; &nbsp; &nbsp; 我希望有高校因为在教育产业化中“大干快上”而破产。截止2005年底，全国高校负债2001亿元。有学校老师自嘲说，我们人均负债20万。若学校不为此买单，就是财政为此买单，归根到底则是纳税人为此买单。当然，把板子都打到学校身上，也不全公平，在“大干快上”的背后，是从中央到地方政府的各种影响、诱导乃至干涉。日月掷去，总得有人为此付出代价。<br />
<br />
21、&nbsp; &nbsp; &nbsp; &nbsp; 我希望政府或下属机构不要再偷偷关起屋子来搞各种“排名”，以让中国有几所大学跻身“世界百强”。要进百强，得靠真正的实力与进步，得尊重客观评价标准。否则，只会像中国科学院推出的“中国国家责任指数世界第一”那样的报告，沦为长久的笑话。<br />
<br />
22、&nbsp; &nbsp; &nbsp; &nbsp; 我希望中国的高校真正拥有学术自由和批判精神，成为“追求真理的民主机构”。60年后，大学不再是迎合某些利益集团的政治的婢女，不再充满兴高采烈通过政审获得项目资金的人物；也不再是与世隔绝的密室，不再充满在呓语般的伪学术话语体系中打飞机的人物。60年后，大学成为孕育创新者、批判者的大本营，从教师到学生，都可以尽情追求智力、真理和美。在其间，人们酝酿自由思想并表达自由思想，创造有价值的学术文化成果并以之推动社会进步。而大学，也因之焕发永久的生命力，站在批评的最前沿，站在创新的最前沿，成为国家社会当之无愧的“轴心组织”。<br />
<br />
