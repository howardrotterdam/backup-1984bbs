<br />
他已经孤注一掷，反正剩下最后两年了，只求给历史一个交代。但孤注一掷换来的只是主流媒体的沉默，以及一些知识精英故作高明的嘲笑。可悲。<br />
<br />
我不认为他的真诚和理念有问题，他的问题只是确实太弱势，除了喊话，的确做不了什么。但在这铁壁合围的时代，有他在那个位置上那么一喊，也是一个突破。就这个意义上说，喊话就是作为，喊话就是行动。<br />
<br />
对那些嘲笑他喊话的一些精英，我很失望。我是做媒体的，我亲身体验了他喊话的作用。近年媒体话语的很多突破，公共话语的很多突破，都是他喊话喊出来的，他先喊了媒体才有机会跟上去，他撕开了裂缝媒体才进而扩大战果。可很多知识精英只是嘲笑。中国就是这种自作聪明的人太多。<br />
<br />
话说回来，温有他自己的问题。家不齐，何以治国平天下，这是他最大的软肋。仅仅公开场合不带家眷是没用的。可能，将来只好效法前CEO，主动将家族的问题提出来，通过公正程序解决，才有明心志而取信天下。<br />
<br />
中国不可能出现单个人的戈巴，因为九零年代后体制上最大的成功，就是完善了反戈巴体制。任何坐到那个位置上的人，若稍有异心，就会被当作戈巴掀翻。除非在巨大社会压力下，出现集体戈巴或会有用。所以对温指望过高本来就不切实际，他能做到现在这样已属不易。<br />
<br />
我不认为中国的问题能只靠一种力量来解决，肯定需要合力。合力中一个极端重要的力量，就是体制内的力量。很难设想，中国的问题能撇开那个拥有七千万人的组织来解决。那么对体制内的任何一点积极的迹象，都应欢迎鼓励，寻求合作可能。而不是一味骂，骂倒一切。但如果连温都不能容忍，还谈何合作和合力。<br />
<br />
