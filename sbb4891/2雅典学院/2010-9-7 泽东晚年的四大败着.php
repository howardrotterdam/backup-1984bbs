													<h2>泽东晚年的四大败着</h2>
						
						
																											<div id="postmessage_817082" class="t_msgfont">为何毛泽东死后尸骨未寒，中南海就血雨腥风地发生足以和&quot;西安事变&quot;相比的宫廷政变，导致中国政局随之发生了一百八十度的大转弯？此后不过几年时间，政权又落入搞权贵资本主义的邓矮子手里，无数革命先烈抛头颅、洒热血才换来的红色江山一夜变色，自从中国&quot;走资&quot;以后,中国工人阶级就沦为中国社会最底层，最没有人权的人物，工人们处境悲惨，形同奴隶。那是中国宪法中明文规定的响当当的领导阶级。劳动人民吃二遍苦受二茬罪，辛辛苦苦几十年，一夜又回到解放前。我看主要原因有以下几点。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;(博讯 boxun.com)<br />
<br />
&nbsp; &nbsp; 1。文革最大的失误就是放过了邓小平和朱F基。他老人家一直对邓小平另眼相看，认为他有才能，还天真地让他复出，毛泽东还不知道邓小平是忍辱负重，伺机复仇，毛泽东是没有完全看清这个比刘少奇还要阴险的人。毛泽东没有学朱元璋，朱元璋杀光了功臣，得以保住朱氏王朝二百多年。毛泽东太慈悲学不了，也应该学宋太祖酒杯释兵权，换上忠于社会主义的年轻一代人。那些追随共产党搞革命的高级干部都是在旧时代里出生和成长的，这就决定了他们尽管是开国的功臣，但不可避免地带有旧时代的烙印，例如建国后被枪毙刘青山和张子善都是经历二万五千里长征，身经百战的高级仗领。当毛泽东发现党内这些人要搞修正主义，骑在人民头上作威作福时，毛泽东就沿用以前的斗争形式来解决问题，不惜抛开几十年的友谊和他斗争，先是高岗，后有彭德怀，刘少奇，林彪，邓小平。他以搞文化大革命的形式来教育人民，鼓动人民抽斗走资本主义道路的当权派，表面上轰轰烈烈，实质上效果不好，不但引起官僚阶级的强烈不满及仇恨，更不幸的是除了上海以外其它地方的造反派及人民群众也没有真正掌权，更多的走资派是这天批斗完后第二天又让他继续工作，毛泽东妄想依靠左，中，右三派的势力维持局面，这就为以后的失败埋下伏笔。最后修正主义者们下去了，毛泽东也斗得累死了，红太阳终于下山了，糟糕的是他没有指定继承人，身后留下了复杂的政治势力，结果右派联合中间派发动政变搞掉了左派。毛泽东这个大漏洞被邓小平都看在眼里，记在心上，斗倒华国锋掌权后就以年轻化为理由，废除终身制，把非邓派或是安排退休，或是靠边站，由上至下都换上清一色忠于邓小平的人，例如，江泽民，朱F基，乔石，李瑞环，胡锦涛等，还一口气指定了两代的继任人，彻底把毛派翻盘的希望打碎，把邓小平的改革开放路线延续下去。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 2. 毛泽东没有搞民主，他搞的“四大”是毛式民主，不是真正的民主，要是中国有真正的民主，后来走资派根本不会上台。即使走资派上台，毛派还有做反对党抗争的机会，不像现在那样连发言的机会都没有，毛派一旦组织活动还要像解放前的地下党那样秘密地小心翼翼，一旦暴露就会落得个被当局抓捕的下场。假使毛泽东泉下有知，他也一定后悔没有搞西方三权分立的民主制度。相比之下，不丹国王就高明多了，他忧国忧民，担心国家前途，自愿还政于民，他将全部权力交给了首届民选政府。他怕如果绝对权力落在一个不贤不肖子孙手上，国家未来则堪虞了。他问过他的子民：“今天你们有一个好国王，如果明天有一个腐败、堕落的国王，那你们又将如何？”。再看台湾也是因为蒋经国开放民主，可以成立合法的政党，搞全民普选，国民党即使一时下台，但现在国民党不是重新上台了吗？要说刘少奇，彭德怀及林彪等和毛泽东有分歧和过节，但叶剑英是毛泽东几十年的战友，毛泽东还把他评为元帅，要知道叶剑英是十大元帅之中唯一没有直接打过仗的。毛泽东对华国锋可谓恩重如山，提名为国务院总理，汪东兴一直是毛泽东的忠实卫士长，还有毛泽东提拔为主持军委日常工作的陈锡联。但为什么几个毛泽东亲密的人在毛泽东死后都要迫不及待地抓捕毛泽东的夫人江青等四人？可见毛泽东身边的人大多是靠不住的，为了争权什么事都干得出来，唯一靠得住的正是法治和民主。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 3。看错了华国锋，他不但缺乏政治头脑还是块辅不上墙的烂泥。先不说他是受叶剑英的挑拨离间还是为了争权而抓“四人邦”这件事，后来他不顾陈永贵苦口婆心的反对而执意让邓小平复出，足以证明他的政治眼光连陈永贵这个农民都不如。华国锋简直就是张学良第二，二人都扭转了当时的局势，深远影响了中国日后的走向，没有张学良就没有共产党的胜利，没有华国锋中国也不会走上权贵资本主义的道路。不同的是张学良因救了中共而被中共视为大恩人，却被国民党视为千古罪人；而华国锋则左右不是人，被毛左派唾骂，他力保邓小平复出却被邓小平夺权，最后被右派抛弃。<br />
&nbsp; &nbsp; <br />
