　宫里的人有时候也特别奇怪，可能是吃了腻味，也可能是厨子竞争力不像外面这么大，所以水准跳水的厉害。大家别以为什么御厨做的菜才够范儿，其实要的就是一个排场。G宴那个难吃，全都是空壳场面而已，要够味道的还得找民间有风味的餐厅。<br />
