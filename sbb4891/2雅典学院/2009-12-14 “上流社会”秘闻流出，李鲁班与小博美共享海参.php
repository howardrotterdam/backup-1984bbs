　　<br />
最厉害的一次，在某次品酒会，把海鲜盅打翻了，弄脏了租来的礼服，据说赔了很多钱。呵呵<br />
<br />
<br />
　其实最经常出现的是太子党。比如某巨头的爱孙，每次来都是开着法拉利的enzo，带着不同的女人来的。有一次有别人倒车的时候碰到了他的enzo，两边吵起来了，后来那个碰了他车的，直接被一派呼啸而来的京V牌照的车，还有几十个黑衣人带走了，最后据说赔了27万了事。其实就刮蹭了一下。我们当时就不明白了，这个世界都是他的，缺这27万么。<br />
　　<br />
　　这个人也奇怪，爱吃各种包子。就是什么都OK，但是最爱吃包子，什么奇怪的东西都要做成包子。每次他来，会有些奇怪的要求，比如说海参馅儿的包子，甲鱼馅儿的包子，还有鹿肉馅儿的，等等。<br />
<br />
<br />
　最奇怪的是有一波人，号称自己是知名作家，我一个好久不读书的人，哪里知道他们是哪个坑里冒出来的。ANYWAY，来者是客都不好怠慢，赔着笑脸恭维着。这些人看上去像个款爷，点的菜都是个倒爷，倒胃口的那种，没事尽爱点那些红烧肉、炒鸡蛋这些没档次的菜，大概是年轻的时候都是穷出身，好不容易有点钱，又想摆阔又想省钱。<br />
