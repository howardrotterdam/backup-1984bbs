　某gj前领导人（被誉为当代鲁班的那位），他自己没来过，但是有他的属下在我们这里预定过海参。有一段时间，每天预定两只一样的，一样的做法，一样的两条。我们当时都很惊奇，因为他老人家年龄不小了，这样吃下去会受不了，太补了。后来才知道，是他吃一只，他养的一只小博美吃一只。<br />
