?我在北京做了N多年餐饮，从小跑堂开始做起，现在混在一个牛B哄哄的顶级会所当客户总监。见识过大大小小的退役政要、上流名人和演艺明星，知道过他们在屏幕之外大众看不到的样子，见多了没有大惊小怪，说句不厚道的甭管他多牛，还不是人，一样要吃饭。<br />
