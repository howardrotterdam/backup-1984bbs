													<h2>好社会弱者必须服从强者 依靠暴政？</h2>
						
						
																											<div id="postmessage_803842" class="t_msgfont">社会必须是一个敬重强者的社会，然后才能恒强。只有敬重强者，才可以建立秩序，在此秩序下，也能保障弱者的一些基本权力。如果不敬重强者...那么，你用什么办法来压服强者呢？你怎么才能比强者更强呢？<br />
你只能依靠暴政。<br />
这也是为什么共产主义的平等理想，只要坚持执行（而不是说说而已），就一定会导致暴政的原因。<br />
我举一个最简单的例子：如果一个部落只有几个人，这几个人遵从其中的强者，这就可以形成一种很好的效率和秩序。如果不遵从强者，搞平均主义，那么强者为什么白白为你们服务呢，如果他没别的地方可去，至少可以偷懒吧？如果弱者要掌权呢？这就是最坏的一种状态，因为他没办法保证自己能够压住强者，也就只能靠谋杀了。就像75那样<br />
<br />
-------------------------------------------------------------------------<br />
<br />
呵呵，上边是nkpoper写的<br />
<a href="https://1984bbs.com/viewthread.php?tid=56556&amp;page=2#pid800635" target="_blank">https://1984bbs.com/viewthread.php?tid=56556&amp;page=2#pid800635</a><br />
<br />
见第75楼 这就是自称崇尚儒家和民族主义者的典型思维<br />
<br />
其实人家nkpoper的意思很明白，要否定共产主义推崇的“平等理想”，就得推崇弱肉强食的等级制度，弱者必须尊重并依靠强者，不要企图推翻暴政，要积极向强者靠拢和服从。<br />
<br />
只不过共产主义所标榜和推崇的也并非美国宪法里所说的“平等”，马列主义鼓吹的所谓“平等”其实是“平均主义”，而他们实行的实际制度其实恰恰正是nkpoper所鼓吹的等级制度，强调的也是弱者对强者必须尊重、依靠和服从，无铲阶级作为最先进阶级有权打倒资铲阶级等剥削阶级，而某某党作为无铲的先锋队更应该起领导作用，等等，其实都是社会达尔文主义弱肉强食主导下的意识形态，与nkpoper所推崇的强者优先论换汤不换药，都是自由民主的敌人。<br />
<br />
一些弱者之所以选择以暴力对待强者，正是因为它们发现这些强者及其奴才们全都鼓吹和遵循着“弱者必须依靠强者”的弱肉强食式法则，在这种社会制度环境下生存的弱者，觉得自己唯一能摆脱强者压迫的方式，就是让强者沦为弱者，反过来接受反抗者的弱肉强食！<br />
<br />
因此一个社会越是将推崇“弱肉强食合理”作为社会准则，则越是无法避免弱者、弱势为了反抗强者而铤而走险，越是会激发弱者们为了适应这一法则而同样以“弱肉强食”的方式如法炮制，这就是中国社会为何总是陷入“治乱循环”、“权力更迭专制不变”的原因，也是7.5发生的根源。<br />
