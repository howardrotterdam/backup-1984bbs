																											<div id="postmessage_631421" class="t_msgfont">燃烧弹做法：<br />
对付一般的车或军用车，一般炸弹就行了，不需要反坦克炸弹。不用炸弹，用燃烧弹也可以烧毁敌人的车辆。<br />
我们要解决两个技术问题，一是在燃烧弹裂开后，燃料如何沾在目标上，不流下来，普通汽油弹，瓶子在敌人车上破裂后，汽油会流到地上，对军车，警车没有多大作用。所以，一定要让他沾住。二是防止敌人用自动灭火器灭火。如果你想烧毁轮胎，轮胎那里一定有自动灭火器。一般是干粉灭火器。<br />
根据以上原理，我们就可以这样制做燃烧弹：<br />
找一个玻璃瓶子，加人几块天然橡胶，再加入汽油，等橡胶融化后，再加入一些面粉，调节稀释度。先作一些试验，盖上盖子后往墙上扔，看看能不能沾到墙上。然后，往旧汽车侧面扔，看看能不能沾上去。然后，加一些镁粉和硝酸钾。这个镁粉就是为了燃烧时，四处冒火星，硝酸钾是防止敌人的灭火器。<br />
可以简单地用一根用汽油浸湿了的棉绳子，用打火机或火柴点燃后扔向敌人防暴车的轮胎或车顶。<br />
如果是攻击坦克，可以加一些铝粉和铁粉。这些金属粉燃烧时，可熔化，把坦克焊住，高温也可能使它工作失灵。所以，没有反坦克手雷或反坦克炸弹，燃烧弹也有可能烧毁敌人的坦克。另外，燃烧时，也可以挡住坦克驾驶员和炮手视线。大面积燃烧，一定会烧出毛病来。如果你们家住在楼，坦克从那里经过，你可以用一个酸菜坛子，装满橡胶汽油面粉团，点燃后扔到坦克上面，一定会烧死。当然，对付坦克最好用反坦克手雷。一般手雷，一般炸弹，枪，炮对它都没有什么用。</div>

							
							
							
							
