<br />
&nbsp;&nbsp;再一次感谢直言提出批评建议的朋友，林昭雕像泥稿是我在接受各方综合意见后修改而成，同仁也可继续研究，亦可有更多的人加入到塑造林昭雕象的雕塑工作中来。目前该雕像主要由本人自费建造，各位如果支持将该雕像安放在北京宋庄农家大院，作为林昭记念堂，欢迎各位给与经济和道义上的支持，我的联系电话6541 7306。<br />
 <br />
 另，我也正在制作设计戊戌六君子的雕像，不久我将公布设计稿方案，我准备选用原来北京铁影壁胡同元代石雕的血色火成岩石材，（我认为这种岩石是平谷的上中远古纪长城系的一种岩石），中国现在太需要的是民主，老在经济上打转是不行的。<br />
