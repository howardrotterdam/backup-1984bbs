<br />
林昭雕像制作过程回顾――――<br />
林昭雕像，我正在制作中……（图）<br />
<br />
 我创作的林昭塑像正在放大中，现在还没有完全作完，还要进行修整。此头像大概一米多高。<br />
<br />
&nbsp;&nbsp;在玻离钢厂做雕塑正赶上那里接了个大浮雕工程，是奥运会内容的工程，因为要给他们多让点地方，一开始我在边上的铁皮小桌子上做，因铁皮小桌有点颤动，都快要做完时，却发生雕像坍塌，现在照片上的塑像是第二次重做的，前后总共用了一个多月了，有一个翻石膏的工人说他给美院的学生翻雕像的时候，人家作钱其琛雕像就用了一天就做完成了。我说人家那是在作“钱”呢，我的这个活是没人给钱的。他听了很不理解说道：不给钱你还做什么？我说我们不不像你们不给钱就不干了。<br />
<br />
这个林昭雕像将来要铸成青铜的估计大约铸造费在两万元，我想在网上征集，将来这个雕像可放在我的思想研究所展出，作为永远的记念馆。同时我还要把迈克尔.杰克逊的同样大的雕像放在用方钢做成的框架上，上面还有用纸做的时尚小人,组成一个整体装置,名谓:“遗忘的、正在被遗忘的；时尚的，和永衡的。”<br />
