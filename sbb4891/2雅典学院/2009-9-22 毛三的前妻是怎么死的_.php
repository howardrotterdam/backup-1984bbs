１９７０年１月１７日生。新中国缔造者毛泽东的孙子、毛岸青与邵华之子。中国共产党党员、中华全国青年联合会常委、北京市西城区政协委员。<br />
<br />
１９９２年７月毕业于中国人民大学历史系；１９９２年９月至１９９５年７月在中央党校理论部攻读硕士学位，后在军事科学院攻读博士学位，２００３年７月份获得博士学位，现在军事科学院从事研究工作。毛新宇1992年毕业于中国人民大学历史系,1995年在中央党校理论部读硕士学位,现在是军事科学院研究员、博士,中校军衔。<br />
<br />
“希望她漂亮、温柔、聪明、理解我”<br />
毛新宇时年37岁，1992年自人民大学毕业时，尚无女友。大学期间，他也有所追求，但均无结果，他将此归因于现时好些女孩太浅薄、不够聪明。<br />
毛新宇的追求无结果，而诸多慕名向他求爱的女子的追求也未成功，原因也是“太浅薄，不够聪明”，“一看来信，就知道是个傻姑娘”。<br />
一次，毛新宇希望一位朋友帮他介绍女朋友，提出三条标准：<br />
一是漂亮温柔；<br />
二是聪明，理解我；<br />
三是最好是中级以上干部家庭出身。<br />
时下正读研究生，据闻，毛新宇大学毕业后，即在有关部门安排下进入中共中央党校攻读硕士研究生课程。据他本人说，这是根据相当高层领导早已做出的决定安排的。他认为这是党和政府对老一辈无产阶级革命家后代的关怀和培养。他对此既感自豪，也感到压力。<br />
