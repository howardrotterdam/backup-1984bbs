													<h2>中国人反腐败就是反儒教反孔子</h2>
						
						
																											<div id="postmessage_451852" class="t_msgfont">作者：秦番 2010-1-25&nbsp; &nbsp;发表于：博客中国<br />
<br />
我们知道，任何有机物都有腐败变质的可能――只要条件具备。一般说，品质优良的物质在同一外部环境下不易变质腐败――仅仅是不易但不是不会；品质恶劣的物质即使在人们认为清洁安全的环境里也会变质腐败。物质的腐败取决于两个条件：外部环境和物质本身的品质。<br />
<br />
人们不会去惩治已经腐败了的苹果，但可以扔掉。这完全是无奈――没有想到改善环境或者根本无力去改善环境。扔掉其实也是一种自罚性质的惩治，至少它表达了人们的愤怒。但是，在环境依旧的情况下，愤怒与事无补；因为腐败的程度这时候往往叫你目瞪口呆，想扔也无处下手。有窍门的果农很少愤怒，也很少手忙脚乱。果农是沉默的，一旦他信誓旦旦，大放厥词的时候，他已经不是一个合格的果农了。<br />
<br />
人的腐败现象并非中国独有，但诲人不倦的儒教文化却是世界上独有的教人们如何去腐败的独特文化。文化既决定人的品质同时又影响社会环境。中国目前的种种腐败现象，就其性质而言，其实也“老早就有”，只不过在物质匮乏，信息闭塞的时代，形不成象今天这样的巨大的反差而已。中国的老百姓历来并不十分看重社会的腐败问题，或者说他们对社会的腐败现象总是习焉不察的。“官”与“民”的日常享用历来总是有别的，这是中国老百姓已经认同了的事实；而儒教则直接了当地认为那种差别是天经地义、不容置喙的。现在人们敢于公开站出来反腐败，这首先是社会进步的标志；同时还说明中国目前的腐败现象比历史上几个特殊时期的还要严重。若不然，我们的老百姓能忍则忍，不会象今天这样，反应如此激烈。<br />
<br />
中国人的腐败不是旁的，它是儒教价值体系生动活泼的体现；孔子功利观一旦实现后的必然产物。趋利乃人之天性，获利的手段也不止一种，但在儒教那里却只有一种――“学而优则仕”。仕则若何？仕则获利；而且“位欲高，财欲厚，门欲大，子息欲盛。”<br />
<br />
孔子虽然嘴上从不言“利”，其实一门心思想获“大利”――“经营天下”之利。这个好梦后来由吕不韦替孔子完成了。年轻时吕不韦曾问其父：“耕田之利几倍？”答曰：“十倍。”（这是类比的说法，中国的耕田者从没获过十倍之利――笔者）又问：“贩买珠玉之利几倍？”答曰：“百倍。”再问：“若挟立一国之主，借以掌握河山，经营天下，其利几倍？”答曰：“不可胜计矣。”<br />
<br />
学而优则仕的人当然有，但学而优则“死”的人则更多――孔子当属此列。假如孔子实现了他的“经营天下”之梦，那么他就没有时间和精力里唆，那么详尽地去区分官吏和庶民日常享用的差异，更没有兴趣去规定他们走路、上台阶的步子应该有何不同。孔子的良苦用心并不是为了什么无关紧要的礼节，他的真正目的在于从根本上划分人的等级差别。正如他自己表白的是为了“贵贱有等”。翻开《礼记》，事无巨细的繁文缛节首先让人感到胸闷气短，但在现实中，人们却把它演绎得丝丝入扣。今天我们各级官员的享用（“待遇”）是有依据的，不信就去翻翻《礼记》。不管你的政绩如何，也不管当地经济发展的水平如何，只要你的级别放在那，车子、房子、票子以及名目繁多的“补贴”就不会少一样，而且是以比法律更神圣的形式年年改进颁布的。<br />
<br />
孔子并没有创立什么宗教，但后人却借题发挥出了儒教。孔子活着的时候人们并不买他的账，他死后很长一个时期也是如此；他的思想仅仅是一种纯学术思想――儒家思想而已。但是到了后来，“种种权势者便用各种的白粉给他来化妆，一直抬到吓人的高度。”（鲁迅：《在现代中国的孔夫子》）所以，“孔夫子之在中国，是权势者或想做权势者们捧起来的。”（同上）如果没有汉代的“独尊儒术”，宋代的“儒学复兴”，（一般认为中国的落后始于近代，但从人类历史发展的一般趋势上讲，中国的落后始于宋代。或者说宋代的“儒学复兴”后，中国注定要落后下去。）孔子的思想不会成为我们每个中国人自觉地修养和行为规范，更不会成为世界上独一无二的宗教。<br />
<br />
孔子强调“仁、义、礼”起初的目的只是为了人们自身道德的提高，以便实现个人理想。我们一直说儒家思想中还有点进取意识，指的正是这一点。但是到了后来，这种本来只属于人与人之间关系范畴的伦理发展成了涵盖一切的规律，它和宇宙规律、国家制度相重叠，又与法律制度、人生哲理、宗教迷信相联姻，形成了一个既有强制性法律，又有自觉性修养，还有恐吓性鬼神报应来保证实施的行为规范。把人一个个教化成无欲无向的神，因而便于统治，这就是儒学礼法名教成为宇宙规律，成为中国人宗教的根本原因。<br />
<br />
孟德斯鸠在探讨儒教对中国人的重要性时指出：“中国人把整个青年时代用在学习这种礼教上，并把一生用在实践这种礼教上。”（《论法的精神》）至于中国人为何那般执着，他没有进一步说明。但作为中国人，我们还需要别人说明吗？我们都清楚，要想实现人生的价值，如果不如此那般，我们几乎无路可走。鲁迅也指出：“能像中国的愚民那样，懂得孔夫子的，恐怕世界上是再也没有的了。”“愚民”尚且如此，就不用说手捧经书的人们了。<br />
<br />
人人都想仕途通达，人人都想升官而后发财，至少通过升官发财，活得比普通百姓舒服一些――这是一般中国人的生存心态。没有必要羞于承认这一点。中国人也是人，也有追求幸福的权利，再说儒教的“正修齐治平”也有其光明磊落的一面。指责中国人的生存心态是毫无道理的，因为在指责的同时你必须指给人们一条别一样的阳关大道来，但你却不能。<br />
<br />
儒教之外的任何宗教只不过是人们的某种精神依托，充其量涉及到了政治和哲学，而儒教则具有包容一切的功能。它既是中国人的人生、自然哲学，又是政治理论依据，常常又和国家形态、法律制度、封建迷信相联姻，（儒教中的“国”就是“家”的放大，“君臣”关系就是“父子”关系的投影；儒教的“礼制”中就有“礼入于法”之说，所谓“刑不上大夫”便是例证。现实中，尤其是在执行法律的过程中，“礼”和“法”常常混淆不清。）这才使它成为世界上独一无二的不信还由不得你的宗教。<br />
<br />
中国人青年时代学习儒教是一种不自觉地行为，后来毕一生精力去实践儒教，带有自觉和不自觉的两重性。不如此这般行吗？不如此这般也就意味着你放弃了一切。也就是说在儒教的现实里你无立锥之地。<br />
<br />
现实里，儒教不象其他一些宗教那么抽象；儒教信仰也和那些不可捉摸的超自然神秘事物无关；它相当具体，具体到我们每个中国人的日常消费、衣食住行和男女情爱当中。“仕”与不“仕”就有天壤之别，“官商”与一般的商人也不可同日而语。其实在儒教的现实里，纯粹市场经济意义上的商人无法生存。<br />
<br />
儒教是不信仰超自然神秘事物的宗教，但这并不说明它比其他信仰超自然神秘事物的宗教合理些。在中国，根本不需要什么超自然神秘事物供人们去信仰，因为儒教的伦理关系本身就是一种强制性的信仰。“天有十日，人有十等。下所以事上，上所以共神也。故王臣公，公臣大夫，大夫臣士，士臣皂，皂臣舆，舆臣隶，隶臣仆，仆臣台。”（《左传》昭公七年）假如你只是一个“台”，不必灰心，通过学习，通过实践，你也可以升至“仆”升至“隶”……<br />
<br />
由此，我们看到，在儒教的现实里，人们自下至上互相信仰反过来又互相践踏。虽然今天“下”和“上”的称谓变了，但中国社会金字塔式的信仰和社会结构并没有变。人人即神人人都有特权。“自己被人凌虐，但也可以凌虐别人；自己被人吃，但也可以吃别人。”（鲁迅《灯下漫笔》）我们已经习惯了凌虐和被凌虐，吃与被吃，拿与被拿，卡与被卡，总之已经习惯了特权的无处不在。<br />
<br />
说起今天的廉政建设，有识之士指出：不可忽视基层干部的腐败现象。此言极是。因为在中国，腐败不光是某一阶层的专利，它普遍存在于我们社会的各个阶层甚至最低层。这不单单是体制上的问题，而是儒教文化生动活泼的体现。<br />
<br />
其他宗教和儒教最大的区别在于：前者要求人们老老实实按人的法则做人，后者则要求人们变成非人――互相信仰的神。<br />
<br />
学而优则仕。在儒教这种功利观的诱导驱使下，天下人心不旁骛，拼命往一条独木桥上挤；为了获得更大的利，拼命往更“上层”爬。范进中举时的面孔告诉人们，利的诱惑是多么的不可抗拒。虽然我们天真烂漫的小学生、中学生们还没有想过“仕”与不“仕”的人生命题，但社会、家庭、学校、书本乃至空气当中随时都能听到孔夫子健康的咳嗽――“学也，禄在其中。”那些吓坏了的孩子们仅仅因为学习成绩不好或考试不及格而离家出走，甚至自杀的事也时有发生。奇怪吗？既然有宗教，就必然有它的殉道者。“儒教吃人，并且这人肉的筵宴现在还在排着，有许多人还想一直排下去。”（鲁迅《灯下漫笔》）<br />
<br />
今天不是又有人扛着“弘扬民族传统文化”的大旗，提出要“振兴儒学”吗？不是又有人多次唱出“传统文化与人本思想”这样的献辞了吗？<br />
<br />
人们痛恨腐败，但是仔细想想，在儒教的功利观、等级观、“礼法”观不仅没有淡化而且更趋天经地义的今天，我们怎么能指望人的公正廉明？你我在痛恨腐败的同时是不是自觉不自觉地跟着一些儒生在瞎吆喝？<br />
<br />
近几年，人们使用最多的一个词就是“以权谋私”，但是在儒教的现实里，说“以权谋私”和说“以嘴吃饭”一样，是废话一句。儒教的价值观里，“仕”和谋权都是手段，获利才是最终目的。这其中的道理就是中国的“愚民”也无师自通，更何况教育空前“发达”，儒生们为“儒”摇旗呐喊的今天。<br />
<br />
还有一个动宾词叫做“升官发财”。这个词却不是人们凭空编造出来的。从行为上说，“升官发财”和诸如“上山打猎”、“下海捕鱼”之类的人类有目的的活动一样，没有任何区别。本来，不管上山还是下海，不管耕田还是贩卖珠玉，只要肯干实干，都是有利可图的；但是在儒教及儒教的现实里，唯有升官才能获得比干其他任何行当都要多的“大利”并且光宗耀祖。为了生存，人们固然还在上山、下海、耕田、贩卖、唱歌、跳舞、救死、扶伤……但心里都清楚，还有一种更好的行当叫做：升官。<br />
<br />
在市场经济的最初几年里，人们天真地认为只要苦干、实干就可获利。于是在90年代初，一大批抱着铁饭碗的“干部”也“下海”了。下了才知道，在中国，只要想获大利，离开了官就无从谈起。活生生的现实在戏弄人们同时也告诫人们：还是走孔夫子指给你们的路吧……<br />
<br />
一年胜过一年的报考公务员热仅仅是因为就业压力吗？今天的公务员为何不象前几年那样轻言“下海”？因为人们渴望的“市场经济”并没有真正建立起来。新儒家们的再一次粉墨登场从另一个侧面说明：在儒教文化下建设“市场经济”要比当初人们想象的艰难得多；同时还说明，如果忽略了文化重建，单纯的经济建设就会变样、走形。 <br />
<br />
按理说，今天是权贵者和权贵者豢养的新儒家们该知足闭嘴的时候了，因为在市场经济的建设过程中，他们并没有象他们当初担心的那样失去什么，相反，他们得到了以往他们不敢想象的物质财富。那么，他们为什么还不闭嘴？答案只有一个：这样的市场经济不正常。要维持这样的不正常，也只有靠儒教。这一点他们比“民”、“众”清楚。那些想做权贵者而不得的儒生们当然更清楚这一点，而且他们还清楚，一个国家、一个民族不可能一直这样不正常下去。有了危机感的人就不会沉默，新儒家们也不例外。于是，就有了拉大旗作虎皮的“弘扬”；于是就有了子虚乌有的“人本思想”、“传统美德”这样呓语的般献词。<br />
<br />
中国的耕田者是清白的，悻悻学子们是清白的，是儒教教他们腐败与堕落；一旦他们手捧经书，奔向“仕”的时候，他已经堕落了；当他们告别田野、教室的时候腐败也尾随而至。有些时候，中国人的腐败还叫人感到同病相怜。<br />
<br />
为什么说中国人的腐败叫人感到同病相怜呢？因为在儒教的现实里，“下”总是要事“上”的。中国人要事的“上”可不是烧烧香、磕磕头或者在胸前划划十字就能打发的虚幻事物，中国人要事的“上”是一个个有血有肉、有情有欲的大活人。很显然，两袖清风者事不了这样的“上”，所以他才两袖清风；廉洁者没有资本事这样的上，所以他才廉洁得自身难保。<br />
<br />
事“上”的方式因时代的不同而不同。今天， 它就是赤裸裸的金钱交易；事“上”也是一种投资行为，本质上和商人的投资没有区别。既然是投资，收回成本在情理之中，获取更大的利益才是当初投资的真正动力。“投资――收回成本――获利”这一过程，在商人那里叫经商；在官员那里叫贪污、腐败。问题是经商可以为社会创造财富，贪污、腐败正好相反，听之任之，它会亡国、亡党。<br />
<br />
法制不健全，体制不完善，（法制在儒教的现实里很难健全，就是健全了也很难健全地执行；体制的不完善正好和儒教的“礼法”观不谋而合。）固然可使腐败者有机可乘，但是如果没有儒教的“理”和“大人言”对广大民众的愚弄和恫吓，腐败者断不至于于那么目中无人：众目睽睽之下信手拿走国家财产轻巧得如同自己囊中探物，自然得如同打了一个哈欠。民众算什么？在儒教的等级观里，“民”从来就是“盲”的代名词。因此，中国“老早就有”现在愈演愈烈的种种腐败现象不仅有其深厚的文化背景，而且还有着广泛的“群众基础”。<br />
<br />
不要指望通过“惩治”就能解决腐败问题，因为“惩治”腐败者和扔掉烂了的苹果一样，是愤怒而不是办法。可以断言，只要儒教的功利观还行之有效，中国式的腐败还会水涨船高。不管采取怎么样的“惩治”措施，人们逐利的天性不会改变。逐利是合理的，只不过中国人逐利的方式有些独特罢了。<br />
<br />
既然逐利是合理的，我们又没有一个除了“以权谋私”和“升官发财”之外更好的获利渠道，那么我们有何理由去反腐败？<br />
<br />
反腐败实际上是在反儒教反孔子，反我们自己的传统文化。指望中国人不腐败，那你必须指给中国人一条别一样的阳关大道来，但你能吗？<br />
<br />
