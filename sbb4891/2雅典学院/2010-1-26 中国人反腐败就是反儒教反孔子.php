<br />
孔子只是个被皇权宠爱的文化名模<br />
文章提交者：猫在阳台 加帖在 猫眼看人 【凯迪网络】 <a href="http://www.kdnet.net" target="_blank">http://www.kdnet.net</a><br />
<br />
儒粉有一个错觉，以为孔子的伟大是靠真实公平的竞争得来的。 错了。 正如白岩松所说：“就是一条狗，整天在央视露面，也成了中国名狗了。很幸运，我是这样的狗” 孔子一个学者，有点小名气，如果不是被汉武帝相中，也就沉默下去了。 他的名气，来自皇帝的政治广告。皇帝看中他，自然是他有符合皇帝心意的顺从美德。一个人，被广告了上千年，名气自然大。 中国只有孔子名气大，是思想界的耻辱。他不是中国人的代表，是皇帝的政治广告模特。他真实的面容是一个认真的学者，一个爱好周礼的顺民。他的思想之所以对中国人影响大，是皇帝洗脑的结果。帝制一倒台，孔子就玩完。 所以，儒粉的错觉，以为孔子是因为自己伟大才著名的，不知道他是因为自己的缺陷而被相中才被皇帝捧出了名声。 <br />
他是一个古代的学者，如此而已。他的观点有对，有错，如此而已。他代表他自己，不代表中国人，如此而已。儒粉认他做老师，是儒粉的自由，但不是中国人的义务，如此而已。思想是自由的，批判是自由的。喜欢他，是你的权利，讨厌他，是我的权利。别用自己的喜好当全民族喜好，别用自己的老师当全民族老师。&nbsp;&nbsp;你喜欢的，只是你自己喜欢的，如此而已<br />
<br />
皇帝为什么喜欢孔子，要把孔子推销给人民？ 是为了人民吗？ 当然不是，是为了他皇家的利益，为了他个人的喜好。所以，他只拣自己喜欢的，对自己有利的。 所以，孔子要求个人修养好，仁慈对百姓，他是不会听的，他认为那是用来赞美他的，而孔子要求人们级级顺从，他是相当喜欢的，就让大家当作经典遵奉。孔子挺风光，也挺冤枉。 但他的思想的确是有剧毒的，所以，才能被专制皇帝相中。<br />
<br />
<br />
知道了皇帝为什么捧孔子，孔子的名气来自哪里，就不会对孔子那么害怕了。儒粉，你们也别以为孔子的名气来自思想的伟大，恰恰上来自思想的糟粕。皇帝爱孔子，肯定不是爱他的”己所不欲，勿施于人“而是爱他的”己欲立立人“<br />
<br />
千年来，只有一个孔子独占思想界的名气，怎么会是中华民族的光荣呢？ 明明是耻辱。一个伟大的民族，应该是有繁星般众多的思想家才对，怎么只能有一轮孤独的人造月亮垄断思想的天空？ 当我看见有的人不以为耻反以为荣地问：”孔子不伟大，怎么还能影响中国人上千年时“ 我不能不哀叹：”傻子呀，孔子能影响中国上千年，是因为中国被专制践踏了上千年。 帝制 一倒，，孔子何存？ 他替封建专制背了那么多黑锅，成为人们痛恨的对象。当然，他也受益了，背黑锅很合理。“ 专制皇帝如果喜欢的是另一个人，另一个思想，他一样会和孔子一样著名。 著名的悠久的影深远的不是孔子的好品质，个人修养了，爱好学习了，而是专制皇权的流毒和儒家思想的糟粕<br />
<br />
<br />
省省吧。 孔子，一个古代的学者，一个专制的顺民，离开了专制的吹捧， 有什么神圣可言？ 有什么师表可语？ 你自己喜欢自己喜欢好了，别说他是我的老师，我以他独占思想为巨大的耻辱。</div>

							
							
							
							
