													<h2>长平：“扔五毛”与抗议的权利</h2>
						
						
																											<div id="postmessage_583768" class="t_msgfont">云南省委宣传部副部长伍皓到人民大学演讲，一名青年男子走上讲台，掏出一叠面值五角的人民币朝他扔去，并大喊：“伍皓，五毛!”随即扬长而去。此事成为推特和微博上的热门话题。<br />
<br />
除了伍皓的名声之外，大家热议此事，还因为它在中国比较少见。不少人甚至不知道怎样定义它，有人说是“突发事件”，有人谓之“扰乱秩序”，有人称为是“没有教养的举动”，还有人认为这是违法行为。伍皓本人倒是清醒，据报道，他在演讲过程中，还举着桌上一张五毛纸币说，这就是刚才网民的抗议。<br />
<br />
这就是一起抗议行动。针对政治人物的抗议活动，是公民社会的家常便饭，可惜我们吃得太少，几乎不记得它的味道，甚至不知道它是什么东西。伍皓当天在微博中说：“我们要习惯在民主的氛围下各尽其责，要学会尊重公民的表达权利，包括各种的表达形式都应得到尊重。”这个说法是相当到位的，可惜他在后来的媒体采访中强调这是“非理性”的行为，甚至认为抗议者“素质低”。我知道，人们对伍皓的抗议之一，就是他说一套做一套，或者做一套说两套，具有欺骗性。他这些矛盾的说法又成了新的例证。<br />
<br />
就其本质而言，抗议是一种言论表达。只要宪法承认了公民的言论自由，也就不能不保障抗议的权利。抗议有各种方式，或言语，或行动，甚至沉默。它可以很喜剧，比如近日广州大学生以“快闪”的形式，到几个地铁站抗议整形广告。它也可能成为悲剧，比如各地拆迁中出现的自焚现象，就是在绝望中的以死相争。由于很多人尤其是官员们习惯了没有抗议的政治生活，是一些“抗议盲”，所以才会认为抗议是“意外事件”，是“扰乱秩序”的行为，甚至说出自焚者是“法盲”这种荒唐的话来。<br />
<br />
秩序并不是神圣不可侵犯的东西，抗议就是要在一定程度上扰乱秩序，破坏稳定，以期引起社会的关注。如果把秩序理解为寻找平衡，而不是死水一潭，那么也可以说，有抗议的社会，才有真正的秩序。看见民主社会里议员在议会中争得面红耳赤，甚至大动拳脚，总是有人感慨说，这样的民主，不要也罢。相比之下，我们的官场，的确要有秩序得多，要文雅得多。但是，这样的秩序和文雅，是什么换来的呢？那就是节节攀升的维稳成本，比比皆是的官场腐败，居高不下的公款吃喝，千奇百怪的派出所死亡方式，和拆迁现场的自焚及活埋惨剧。<br />
<br />
有人说，伍皓无论作为一个“六品大员”，还是一位演讲嘉宾，朝他抛纸币都是对他的不尊敬。抗议中尽量尊重被抗议者的人格，这的确是一种文明行为。但是，假如为了尊重，干脆不允许抗议了，这又是对现代政治文明和公民权利的不尊重了。最近英国有一个判例，让一些因为扔鞋抗议而卷入官司的人有望解脱。法院认定，“扔鞋”只是抗议示威中的一个例行的作为，并不属于暴利犯罪的范畴。一位法官表示，“扔鞋”不应该成为指控实施暴力行为的证据，它只是一种象征性的政治姿态。<br />
<br />
对于“扔五毛”的另一种批评认为，针对伍皓的抗议没有太大的风险，“有本事你去抗议更高级别的官员啊”。本来这种批评不值一驳，但是它在话语市场中还相当畅销。事实上，任何抗议对象都不可能是最大的“恶”，甚至根本不是“恶”，只是意见不同而已。假如只能对最大的“恶”抗议才有意义，那么这个世界上就不存在抗议活动了。抗议作为一种公民权利，并不附带这样的条件，只要有人感到不满，他就可以表达相应的抗议。同时，任何抗议活动，都要进行风险评估，这不是什么投机，而是一种基本的理智。在进行了风险评估之后，仍然为了心中的正义选择冒险抗议的人，自然受人敬仰；但是，那些在抗议中进行自我保护的人，也不应该受到谴责。<br />
<br />
<br />
因此，还有一种听起来很严重的指控，仔细想来近乎无厘头了 ――那就是声称这起抗议活动“有预谋”、“有组织”。在抗议活动中，我们总是习惯于把“预谋”和“组织”当作反义词来说，似乎现场突然冲动更好。其实，世界上大多数活动，尤其是抗议活动，都是有预谋与组织的。这本来是理性的表现，却又被认为是非理性的表达。<br />
<br />
最值得重视的，似乎是这样一种意见：伍皓是官员中的开明派，应该保护起来。且不论伍皓到底是不是“五毛”，也不论自古以来盼“明君”、“贤臣”的谬种流传，单就以“保护好人”为名来压制别人抗议权利的企图，就不是一种赞赏开明的做派。中国社会只能在抗议声中成长，而不是吹捧什么好人。假如真有所谓开明的官员，应该为自己“以身作则”感到骄傲。<br />
