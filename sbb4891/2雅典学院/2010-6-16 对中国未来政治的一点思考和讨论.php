													<h2>对中国未来政治的一点思考和讨论</h2>
						
						
																											<div id="postmessage_686226" class="t_msgfont">本帖仅为抛砖引玉之用，内容欢迎讨论，但谢绝人身攻击和言语暴力<br />
<br />
（本楼只是第一部分，本想写第二部分，但是后面和几位朋友的辩论中已经大部分讨论到，恕我偷个懒，大家自己爬楼吧）<br />
<br />
话说写这篇文字并不是心血来潮，是因为一次很有代表性的对话：某天半夜和室友聊天谈政治，室友说：就算现在共党倒台，也没有一个政府、一个制度能够代替它。<br />
<br />
当时我并没有回答他，不过我知道答案，因为事实上“如何打造未来的中国”这个命题早在100多年前就早已有人在研究，并且在65年前就已经有了大致的答案。<br />
<br />
众所周知，中共灭亡，不是“会不会”的问题，只是“什么时候”的问题。不过所谓不破不立，如果要真正地推翻中共，在行动上打破现有的体制与政府的同时，在现体制倒下之前必须要完成对于新体制的构建工作。<br />
<br />
如何打造未来的中国，其实主要的目标和精神在零八宪章里也说得很明确，我也不需要过多重复。而在具体的落实上，目前真正地成系统地思考和总结的可能只有孙中山。孙文先生在政治体制上的很多设计现在读来依然令人叹服。而之后民国的诸多实践也给我们很多生动的例子和教训。<br />
<br />
我整合了一系列的思考和前人的智慧，最终形成了我自己的答案。或许很傻很天真，但这也仅作为是一个公民的思考吧。<br />
<br />
第一部分：政府架构<br />
<br />
对于未来整个政治体制的核心精神，我觉得我不需要多废话――民主，自由，人权，宪政。而至于如何构建未来的中国政府这个问题上，我相信有不少人的思维可能只停留在简单的“三权分立”这个概念上。不过很明显，三权分立只是一个表象，分权制衡精神才是这个政府结构的核心精神。分权制衡，分的未必是三权（民国就是五权），制衡也未必需要完全分开（议会制国家行政立法关联），这个思维局限我觉得必须要突破。<br />
<br />
而在如何分权制衡上，东西方各国都已有相当多的实践，美国有总统制，德国有议会制，法国有半总统制，英国有威斯敏斯特制，这些制度各有千秋。不过我个人认为，这些并不一定是最好的答案。总统制行政效率高，但容易引发总统独裁；议会制权力相对平均，但是选举制度设计不当容易引发小党林立和政局动荡；而法国的偏向于议会制的半总统制看似不错但是很容易引发总统和内阁的分裂。私以为最好的答案，其实远在天边，近在眼前――民国的五权分立，偏向总统的双首长制。<br />
<br />
如果各位看过中国宪法之父张君劢先生的《宪政之道》一书的话，就会知道张先生是比较推崇责任内阁制度的。而他对于里史上第一部半总统制的宪法――魏玛宪法，也有着相当高的评价。他在当时起草民国宪法时所设想的分权制衡机制，就是一种双首长制。后来经民国多年的探索和发展，现在的民国政体基本正如张先生大致设想的那样。当然民国的制度在某些方面也有偏差，但是在总体思路和精神上我认为没有问题，再加上结合中国传统政治的监察，考试两权，在某种程度上而言，民国的政府结构设计基础是非常优良的，甚至夸张的说是独步天下的，如果我们对其的改造得当，未来中国政府的民主、高效和廉洁根本不是梦想，中国真正的崛起也将拥有相当的制度保障。<br />
<br />
我所设想的未来中国政府，是一种“四权分立的责任内阁半总统制”，而这里的四权包括：行政（总统+国务院），立法（联邦议会），司法（最高联邦法院），监察（监察院，这里的监察权包含了部分的考试权，原因后面会讲）。<br />
<br />
总统为国家元首，掌握部分行政权，总统对全体选民负责。负责政治上的最高责任，是三军最高军事统帅，代表国家行使职权（主要包括外交、军事、行政、立法、司法等权力），同时可以提名任命文武官吏，但都必须经过联邦议会甚至包括监察院同意及副署。<br />
<br />
国务院为国家最高行政机关，和总统分享最高行政权。总理作为国务院行政首脑，由总统提名，联邦议会和监察院同意任命。国务院对对联邦议会负责，形成“责任内阁”。政府为“小政府”，我的初步设计只有11个部（内政，外交，国防，财政，司法，经济产业，建设交通，科学教育，文化观光，卫生与公共事务，环境资源）和7个左右的委员会，相比起现有的臃肿不堪的国务院，可以说不论从哪个角度而言都是大大地缩小了。如果联邦议会对于国务院全体不信任时，国务院需全体辞职或者提请总统解散联邦议会，但是同一总理不可以再提请解散联邦议会。<br />
<br />
联邦议会为一院制，为国家最高立法机关，掌握最高立法权。联邦议会通过人民投票普选产生（选举制度我个人比较希望是单一选区两票制），议长由议员推选产生，总统和监察院同意并有总统任命。议会负责审查各项法案以及预算，任何法律、条例包括之前总统的提名都需经由联邦议会半数通过，并由总统公布后才有效力。议会可以对国务院不信任投票，当然总统在议长同意之后也可以解散议会。<br />
<br />
监察院为国家最高监察机关，掌握最高监察权。监察院行使对公务员的弹劾、纠举权，对于国务院的审计权，对于全国各级公务员的考核权。监察院院长由总统提名，联邦议会同意。监察院采取委员制，各部的监察委员由各地地方议会选举产生。监察院初步设监察，审计，考选等部。<br />
<br />
这里就要说到为什么要把监察和考试两权合并，首先在中国古代，考试权归于吏部，而监察权归于都察院或者御史台，而在明代的官员考察体制中，所谓的“京察”一般都是由吏部和督察院以及六科完成的。也就是说，虽然人事和监察两权不归一个部门，但是在实际操作上是有机结合的。其次现在民国的体制当中，监察，考试两院几乎悬空，特别是在陈水扁当政时期。我个人认为最根本的原因就在于两院之权过于贫弱，不足以和议会，内阁形成制衡，达到防止总统内阁或者议会强势甚至独裁。而如果将对公务员的考核、提拔和弹劾审计职权合一，监察院的权力就会大大加强，按照张君劢先生的设想就是监察院和一个一院制议会形成一种新式的“两院制”，形成国务院――联邦议会――监察院的三足鼎立分权制衡。<br />
<br />
最高联邦法院为国家最高司法机关，掌握最高司法权。联邦法院大法官由总统提名，联邦议会和监察院同意任命，终身任职。最高联邦法院负责违宪审查以及司法审查权。<br />
<br />
