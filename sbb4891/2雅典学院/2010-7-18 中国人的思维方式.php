													<h2>中国人的思维方式</h2>
						
						
																											<div id="postmessage_747113" class="t_msgfont">中国式思维方式的严重缺点在于轻视量化和严格的定义，没有严谨的推理，也不注重寻本求根。甚至定性的描述和思考都很含糊。很多情况下，知其然而不知其所以然；更兼有不知其所以然也不知其然。著名典故有“两小儿辩日”，一个用很简单明了方式提出的问题，解答不了，然后不了了之。既然圣人都回答不了，就成为事实上的不可知论，也就可故作高深地不做探究。<br />
<br />
举例来说，命题“为什么用样本的平均值去估计population（总体样本）的水平？”<br />
<br />
儒家回答：中庸嘛，圣人说了，中是最好的estimator（估值），所以当然用平均值了。<br />
<br />
道家回答：天地分阴阳两极，大道运行其间，population的水平在哪，用什么值来估算其实都无所谓，主要看大道在哪；而道可道，非常道，大道至简，就各自体会吧。<br />
<br />
墨家回答：世间应主张兼爱，不歧视，所以所有的样本都应该等同对待，当然只能取平均值了。<br />
<br />
法家回答：世间当有法，所以不管用什么值，总得有一个值，根据经验来讲，平均值总是八九不离十，所以规定用平均值。<br />
<br />
苏格拉底回答时，会先把上面所有立论全都驳倒，然后建立一套假定，在假定的基础上，推理出为什么样本平均值是最佳estimator。同时会补充说明，所有推理都是建立在假定上的；所以只要其中一条假定变化，则结论就有可能不同。毕达格拉斯看到苏的回答后，觉得有必要深入研究，会带领一班门徒，对各个假定进行验证、比较和发展。越验证和细化，越发现事情越来越复杂，最后居然发展出整套统计学和数理分析学。<br />
<br />
苏的假定是：<br />
<br />
1，假定样本间彼此独立，一个样本被抽到的概率不会影响下一个样本被抽到的几率。<br />
2，假定population的水平有一个期望值，用样本平均值是去估计这个期望值；如果物理过程没有此期望值，则无法用样本平均值估计。<br />
3，严格定义&quot;最佳estimator&quot;的佳是什么意思；定义佳的概念指方差，最佳指方差最小；用平均值为最佳就有了客观标准，而且也可由此给出平均值作为最佳的适用范围。<br />
<br />
至少要有这三条假定，才可以把平均值当作最佳estimator。当物理过程违法了第一条假定时，时间序列模型理论就被提出来。当不满足第二条假定时，权重回归之类的模型被提出来。当不满足第三条假定时，中值、区间值等estimator则应运而生。然后一个又一个新的问题被激发、被解决、被激发、被解决... ...<br />
<br />
简单的问题，中国圣人自以为是地&quot;争辩止于智者&quot;；傲慢无理的权威不容挑战，所以中国人对自然的认识难脱表象、肤浅和愚昧。而在古希腊则引起从数学到哲学的一连串问题和解答；也许最终没有确切答案，但整个思辩的体系成为了人类与自然互动以及人类知识进步的利器。<br />
<br />
<strong><font color="red">在整个中国人的历史上，知识实际上从没有试图发挥应有的作用。读书是因为：书中自有颜如玉，书中自有黄金屋；学而优则仕。即，为了三样东西：女人、钱和权；而不是为了探索自然，然后把知识用以提高精神和物质生活水平，以及在此基础上更好地探索自然。文人热衷于唱赞歌，是统治工具，跟自然知识毫不相干。探索自然知识的概念，即科学，是五四运动时才从西方引进的；解决人与人之间关系的概念，即民主，也是如此；但直到今天，在具体应用上依然差的遥远。</font></strong><br />
<br />
文革时，居然有要打倒牛顿定律的事情。因为牛顿定律是资本主义的定律，要连根拔除。革命小将们要把水从山脚引到山上去，以证明社会主义比资本主义优越。这事发生的不久远，才40年前。40年后的今天，变成什么样了？屈原的故乡有人见死不救，挟尸要钱！<br />
<br />
一有人说中华民族是劣等民族，中国人立刻群情激昂。但几乎从没有人清醒理性地问过：什么叫劣等？什么叫劣等民族？中华民族的定义是什么？凭什么说中华民族是劣等民族？<br />
<br />
中国人整个思维体系上缺乏基本的是非原则观念，造成逻辑混乱，甚至中文语言本身就在科学描述和精确表达方面具有先天缺陷（而以中文为蓝本的日语，则很好地吸收了西方字母语言的理性成分，创造性地发展了方块字的功能）。缺乏基本是非原则观念，导致失却起码的诚信、人格和良知；逻辑混乱导致没有任何理性思维，以<strong>假、大、空为荣</strong>。<br />
