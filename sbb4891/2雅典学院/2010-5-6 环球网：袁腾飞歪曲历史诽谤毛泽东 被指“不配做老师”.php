<br />
<br />
　　在环球网论坛里，“追捧袁腾飞是在毒害中国未来”的专题受到热捧，专题主文“戳穿袁腾飞，还中国历史和革命领袖真实面目”，强烈抨击了袁腾飞有关新中国建国以来历史授课视频歪曲历史、特别是诽谤开国领袖毛泽东的做法，并且得出“原来袁氏的所谓高级，就是建立和体现在肆意歪曲中国历史，恶毒诽谤革命先烈，疯狂攻击中国社会主义制度的罪恶基础之上”的结论。<br />
<br />
<br />
　　网友们在文章后面的评论中，也就这段视频内容发表个人看法，其中谴责袁腾飞的声音占了大多数。网友“除奸队员”的话虽然简单，但一语中的：“小袁改写了历史！！”有网友说，袁腾飞在课堂上发表这样的言论，“不配做老师！”、“他做教师对孩子的毒害是非常深的，因为孩子们不懂事”、“这是在误人子弟”。有网友表示：“我也听了袁某人的一些简短视频，听他那么说，就知道其人不过是为了哗众取宠！只是为了在大众面前撒娇，出名而已。”<br />
<br />
<br />
　　对于袁腾飞在视频中对毛泽东的评价，很多网友都表示愤恨。网友“lichen”说：“袁是什么人，看了气得发抖，我只想问一句他为什么这么恨毛泽东？”“红楼醉客”则说：“历史岂容他姓袁的乱改！！！！伟大的毛主席岂容他随意诽谤和侮辱！！！”<br />
<br />
<br />
　　网友“磕睡猫”在领教了“袁老师”的讲课后“悲愤交加”，称“此人满嘴跑马、大放厥词，除了疯狂污蔑国父毛泽东，更将新中国头三十年说得一无是处。”他表示，自己起初比较欣赏袁氏的讲课风格，觉得他讲的很生动，和一些照本宣科比起来入耳许多，没想到这段视频如此“令人难以想象”。他认为，“毛泽东之于中国，就如华盛顿和林肯之于美国！”，但到了袁腾飞的嘴里，却变得一无是处，“如此颠倒是非，黑白混淆，你于心何忍，居心何在？！”这也正是很多其他网友的疑问，他们指出：“歪曲历史就是背叛！”<br />
<br />
<br />
