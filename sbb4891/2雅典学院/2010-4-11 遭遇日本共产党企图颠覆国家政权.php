　　日本共产党这种固执十分明显，苏联解体后，许多国家的共产党纷纷改名，而日共却坚持不改，志位和夫曾说过：“日本共产党不想象更换衬衫一样改变自己的名称，放弃共产党的老招牌。”他们对其他国家共产党所作的大幅政策调整并不看好，曾表示“二十世纪的世界没有成功的社会主义模式，而日共所要做的就是去探索一个成功的模式出来。”<br />
<br />
　　这种抱着百年前路线不放的做法，在外人看来多少有些不知变通。要知道，不要说政治领域，即便是商界，《幸福》杂志一个世界前排出的世界一百强公司，今天还能留在这个排名中的只剩不到1/10。可见如果一个公司始终按照老辈子传下来的方法做生意，再大，也会在新的竞争对手面前败下阵来。然而，令人惊奇的是，这个日本共产党在日本政界却活得颇为稳定。比如，苏联解体，很多国家的共产党一落千丈，而日本共产党的活动好像没受什么影响，那之前有30多万党员，今天还是30多万；这次“平成大政奉还”，堂堂自民党在众议院的席位一下缩水到原来的1/3，而共产党则一席未加，一席未减。<br />
<br />
　　不过，日本共产党的席位，也一直没有多少变化，支持者的数量，也没有多少变化。在政坛风起云涌之际，在政治史上最有激情的共产党，在日本却是个超级稳定的政党，一直好也好不到哪里去，坏，也坏不到哪里去，真是让人奇怪的事情。<br />
<br />
　　后来做了些研究，才发现，看来奇怪的事情，其实一点也不奇怪。日本共产党虽然政策纲领涵盖从日美关系到和平宪法各个领域，但从本质上说，它始终坚持马克思主义原教旨主义路线的政策，使日本共产党一直是一个代表穷人利益的政党。<br />
<br />
　　日本共产党，为穷人说话，甚至达到穷就是理的地步，看来有些像古代的侠客。<br />
<br />
　　说“穷人”而不说“无产阶级”是因为按照“无产阶级”的标准，今天日本大量的白领，金领和高收入蓝领阶层也是无产阶级，但是他们对于共产党的纲领并不是很热心，他们有较大的择业自由度和通过契约直接向雇主争取利益的能力，并不很需要共产党这样的组织帮助。日本共产党在日本政坛最大的价值是代表低收入阶层的民众争取他们的利益，比如反对无端解雇啦，进行“春斗”要求增加工资啦。日本共产党依靠这些穷人，代表这些穷人，也帮助这些穷人，倒是大有古风。这就是为什么越穷的社区共产党广告越多的原因。<br />
<br />
　　由于低收入阶层总是存在的，所以日本共产党总是有自己的群众。这样，无论政局怎样变换，共产党总是不会彻底倒掉。这也从一个侧面说明了一个问题――在任何一个社会，代表穷困阶层利益的政治团体，一定会存在。如果不能合法地存在，就会不合法地存在。<br />
<br />
　　但是，日本共产党为什么又不是发展得很快呢？其原因也在它的固执。一方面，在日本这样的发达国家，“穷人”的比例是有限的，这限制了日共的群众基础(铁杆群众大约500～600万人)；同时，另一方面日共也不想扩大自己的群众基础，认为如果自己的群众超出“穷人”的范畴，自己就不是共产党了，而变成社会民主党了。这是信奉正宗马克思主义的日共所绝不能接受的。<br />
<br />
　　更让人啼笑皆非的是，日本共产党做得再好也不会增加太多群众。这是因为，如果日本共产党为穷人争利益的工作做得不好，穷人不信任它，它就难以发展；而如果做得好呢，得到利益的穷人如果变成了有钱人，又离开了日共给自己划定的群众范畴。<br />
<br />
　　而这一点，日共亦找不到办法来改变。按照日本共产党的观点，如果代表穷人以外的阶层，那就不是共产党了，穷人们一定会找另一个挂着别的名字的党来代表自己的利益，那日共就要崩盘了。<br />
<br />
　　可是，如果只代表穷人，显然又不可能代表整个国民的利益，这就是其他阶层的日本人对日共兴趣不高的原因。真是一个悖论啊。如此，也许共产党真要变成日本政坛的“万年在野党”了。<br />
<br />
　　作为一个中国人，看看这样一个不是执政党的共产党怎样生存和发展，或许也是一件挺受启发的事情。</div>

							
							
							
							
