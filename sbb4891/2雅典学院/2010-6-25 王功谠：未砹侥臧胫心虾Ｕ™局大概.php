江泽民愿捧屎盆子？<br />
<br />
这些迹象无疑都还很缥缈；可是，胡锦涛要的就是这种缥缈的劲儿。 首先，胡锦涛知道这张王牌的厉害，对自己有没有能力掌握这张王牌，恐怕心里并没有底儿。 全中国谁不明白？ “六四”平反是一个原子弹，一旦引爆，威力无穷，后果很可能是难以想象的。 引爆原子弹的人，首先葬身这一爆炸，那也是很可能的。 其次，胡锦涛针对的目标，是江泽民、 习近平，为此他正在卖力拉拢党内高层其他对江不满意的力量，尤其是李鹏、乔石、宋平、朱**基、李瑞环。 这些人的大多数，至少前三位，都是当年坚决主张天安门镇压的干将，哪能容许有人在“六四“问题上松动？ 只有这种缥缈的显露，才符合胡锦涛的战略目标和政治利益U他并不想重评“六四”，更不想党内高层对他有这个怀疑；他要的就是拿这张牌镇一下江泽民，提高自己应对江泽民力量时的威慑力。 最低限度说，他这样做，第一，战略上，可以引起对手恐慌，造成对手应对失误，从而增大自己获利的机会；第二，政治上，可以帮助胡锦涛在民意层面得分。<br />
就第二点来说，胡锦涛过去几年一直通过在舆论上臭江来争取民心。 现在，七、八年下来了，胡要再像他的第一个任期内那样，把坏事都算到江头上去，不说不可能，也是比以前困难多了。 摸一摸“六四”这张牌，这方面就可以很容易地回到对胡有利的局面。 胡锦涛虽然不读书，但是似乎读过鲁迅的名篇《纪念刘和珍君》，知道那些“苟活者”愿意在“淡红的血色中”“依稀看见微茫的希望”。 一般老百姓不说，党内、社会上那些希望“六四”平反的自由派人士或政治异议人士，特别是那些“两头真”的党内自由派老人，最喜欢抱幻想，常常一厢情愿地分析政治，这些年他们没有少把希望寄托在胡锦涛身上。 对他们眨一眨眼皮儿，他们就以为深意在焉，默契在焉，就以为机会来了，救星来了，于是主动配合，要拥护“以胡锦涛同志为首的党中央”了。 这些人社会影响很大，又特别讨厌江泽民。 让他们去帮胡锦涛讲话，那比官方出面说话，是要远远更能迷惑社会舆论乃至国际舆论的了。 朱厚泽去世，留下一句极为深刻的话U我们这些人的存在，使很多人以为有希望，其实没有希望。 结合我们这里的分析来看，可以说，这种“以为有希望”的心理，最容易被统治者利用，看来还将帮助胡锦涛和他的不管是谁的继任人把中共专制统治延续下去。<br />
就第二点而言，看来也起作用了。 据说，江泽民表了态，说“只要我活着，谁也别想为六四翻案”。 如果这是真的，那江泽民就是这次十八大权争之中的最大笨伯，明显是在胡锦涛看似有意无意的撩惹中犯了巨大的战略错误。 你想，江泽民当年巩固权位的三个重大动作，本来都是针对“六四”镇压的干将的，从陈希同到杨家将到乔石。 江泽民当然没有、也不会为“六四”平反，但他懂得与镇压拉开距离。 现在，胡锦涛刚刚才要虚无缥缈地暗示一下“六四”这件事，江泽民就雄赳赳、恶狠狠地把这事承担下来了，大有一副要捧着邓小平留给他的这个血腥屎盆子走进八宝山的气概。 胡锦涛的十八大困局，看来正在解套。<br />
<br />
