美梦有根据，政治看实力<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;在这个讨厌的世界上，大凡美妙的事情，往往都是梦；不过，根据心理学家们的研究，梦也不是没有现实根据的。 胡锦涛已经找到了不少论据，可以支持他再干一届总书记，到二零一七年的十九大才交班。 千论据、万论据，最根本的一条论据是U江泽民可以，我为什么不可以？<br />
首先，看年龄。 胡锦涛一九四二年十二月出生，到二零一二年秋天的十八大，还不满七十周岁。 而江泽民是什么年龄交出总书记位置的呢？ 老江是一九二六年八月出生，到二零零二年秋天交班的时候，已经超过七十六岁了。 按这个先例，胡锦涛再干一届总书记，到交班的时候也还不足七十五周岁，完全可以证明中国政治在年轻化问题上还是在不断进步的。 哈哈，从十八大到十九大的那五年里，老江即使还能拖着不翘辫子，那也一定起不了床了。 十九大是不是习近平接班，那就将完全由胡锦涛说了算。<br />
其次，看任期。 江泽民一九八九年六月上任总书记，到二零零二年十一月卸任时，已经干了十三年半。 凭这个先例，为什么他胡锦涛就只能干十年，而不是干它十五年？ 就算不能干满十五年，那也干满十三年总是可以的吧？ 江泽民干的那是两届半，胡锦涛即使不能干满三届，不妨也干两届半呀。 反正十八大胡锦涛应该连任，至于什么时候卸任，这个可以商量嘛。 十九大太久？ 那就二零一五年加开一个“党的全国代表会议”，就像一九八五年那次那样，届时胡锦涛交班，有什么不可以？ 说来也是，胡锦涛第一次当选中央委员会正是委员，就是在一九八五年这次不前不后的“全国代表会议”上。 退出中央委员会也在全国代表会议上，中间整整三十年，岂不也是我党一段历史佳话？ 当然，一旦十八大能够连任总书记，胡锦涛的权力陡增，那就不是这套说词所可以约束的了。 到了二零一五年，究竟开不开全国代表会议，那还不是胡锦涛来决定？<br />
第三，看资历。 胡锦涛一九九二年进入政治局常委会，十年之后出任总书记；习近平二零零七年刚刚进入政治局常委会，五年之后的十八大就要接掌大位，那岂不是锻炼不够？<br />
不错，所有这些根据，似乎都很在理。 可是，政治是讲究实力的，并不讲究根据。 你有实力，你的根据就管用，哪怕是牵强附会的根据；实力不够，你的根据就是扯淡，哪怕用的是列宁的观点、马克思的逻辑。 胡锦涛有没有实力，支持自己再干一届呢？ 这就是一个很成问题的问题了。<br />
在中国搞政治，实力首先在军队；“枪杆子里面出政权”，这是毛老祖宗的起家宝典。 其实不光出政权，枪杆子里面也出党中央，也出总书记。 再早不去说他，光说自打毛泽东死后，这共产党内的权力斗争的结果，就从来都是靠枪杆子决定的。 华国锋、叶剑英、汪东兴靠军事政变抓了“四人帮”，于是华国锋成为“英明领袖”；等到掌握枪杆子的叶剑英和邓小平达成交易，再等到当年曾经一下子指挥两支野战军的“邓政委”回到政坛，华国锋只能下台如仪。 邓小平也不去当什么第一把手、总书记或者国家主席，他就是死死抓住军权不放，人人都得奉他是权力核心，或者用陈云的话来说，“是我们的总头子” 。 靠着枪杆子，邓小平搞掉了华国锋，搞掉了胡耀邦，又搞掉了赵紫阳，还在“南巡”的时候差那么一点就搞掉了江泽民。 什么他妈的“党指挥枪”，老子“有枪就是草头王”！<br />
老邓之后，江泽民逐渐抓紧了军权，一直抓到现在。 解放军现役的上将，百分之九十都是江泽民提拔的；胡锦涛当然也不是没有下功夫去笼络军队，提拔将领，控制枪杆子，可是迄今成效平平。 他这个现任军委主席，虽然不是“阳春面”的，但也远远不是“牛肉面“的，顶多有一点儿“木须”在里头，糊弄个小职员的午饭还将就，远远对付不了拳击手的好胃口。 现在，胡锦涛胃口这么大，不仅要上拳击场，而且还想大胜对手，要靠这碗“木须面”就明显力不从心了。<br />
