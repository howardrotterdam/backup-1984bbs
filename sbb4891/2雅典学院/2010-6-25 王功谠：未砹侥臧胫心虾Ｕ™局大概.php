三传传给胡海峰？<br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;胡锦涛要想这样做，他有现成的人选、 顺手的格局。 与当年选拔胡锦涛一枝独秀地出任储君的情况不同，十七大选拔了两个年轻的政治局常委花开两朵作为接班搭档。 只要胡锦涛够狠、够恶、够胆量、够气魄、也够阴谋、够能力，他可以一脚踢开习近平，把自己政治上的亲生儿子李克强扶上来，接自己的王位。 这样可以一了百了，形成父（胡锦涛）传子（李克强）、子传孙（胡春华）、而孙再传子（胡锦涛的骨肉亲生儿子胡海峰）的“三传到位“的胡氏王朝。 比起新加坡李光耀，不过中间在二传手之外还多了一个三传手。 怎么说也就是胡锦涛之后二十年的光景，胡氏王朝的万世基业就牢牢铸就了。 比起胡锦涛所羡慕、钦佩的北韩金氏王朝，弯儿是多拐了几个，但并不曲折，也不坎坷，总的方向更是绝对一致的。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;这并不是说笑话，也不是什么天方夜谭。 老一辈的“太子党”可以接班，新一茬的“太子党”难道就不能接班？ 当舆论还在关注什么薄熙来、俞正声的时候，君不见，李小鹏、胡海峰也都已经纷纷就位了。 何况，当年江泽民何尝不想让江绵恒在政治上多有点儿作为？ 只是江泽民还没有这个本领做得到那个样子就是了。 胡锦涛较之江泽民，青出于蓝而胜于蓝，“软的更软，硬的更硬”，这也不是今天才看得清楚的。 而且，胡锦涛的城府深沉，其野心和能力总是被人低估，也就是说其野心和手腕其实常常超乎人们的估计，这几年也可以说已有公论。 在中国这样一个伟大、复杂、邪乎的国家，发生什么事情都是可能的U不怕做不到，就怕想不到；人有多大胆，地有多大产！<br />
