打不打这张最大王牌？<br />
<br />
政治固然讲实力，同时却也讲计谋。 实力不够，计谋如何就变得更加关键了。 可以说，没有实力有计谋，可以获得实力；仅有实力缺计谋，早晚失掉实力。 江泽民当年有个计谋专家曾庆红帮他密室策划，老江自己也可以说还是个脑子灵光的人，于是乎，整肃陈希同，搞垮“杨家将”， 敲掉特务头子乔石，三拳两脚之后，实力陡然大增。 胡锦涛学江泽民，这三招里面，迄今仅仅走了一招半，甚至连一招半都不到，他今天的实力，比起当年江泽民退休前三年那个时候的实力，还差老远呢。 这也说明他计谋还是不够。 就个人而论，胡锦涛可能比江泽民更为计谋深沉；但是，他那个令计划，可就远远比不上曾庆红了。 什么叫“一招半”呢？ 一招，就是学江泽民搞陈希同，搞掉了陈良宇，这个我们说过了；还有半招，就是学江泽民搞乔石这一招，逼迫曾庆红在十七大上退休――但这一招远远没有学到家，反被曾庆红利用了。 乔石当年灰溜溜地下台，退休之后连个屁也没有留下；曾庆红退休是以一换三，把习近平、贺国强、周永康都搞进了政治局常委。 最最重要的是，习近平由此占据了“储君”地位，成了比曾庆红还要让胡锦涛睡不着觉的心腹大患。<br />
很明显，这里缺了江泽民当年的第二招，即整肃“杨家将”那一招，所以胡锦涛直到今天对于军队的掌控还很成问题。 第三招也使得后患无穷，可以说是顾了当时的十七大、赔了长远的十八大，造成今天胡锦涛的困局。 我们说胡锦涛学江泽民甚至连一招半都不到，还不光指的是这些。 就是第一招，胡锦涛其实也没有学到家。 为什么这么说呢？ 单就权位而言，陈良宇无疑就是陈希同第二， 胡锦涛搞掉他，也就立威了，这没有错。 可是，陈希同还是六四镇压的功臣，手上沾着北京学生的鲜血；江泽民搞掉他，不仅党内立威，而且向外界巧妙地传达了某种言外之意。 胡锦涛搞掉陈良宇，除了权位还是权位，可就缺少这后半截的深沉政治含义了。<br />
事过数年之后，到了胡锦涛犯愁十八大的今天，他似乎才刚刚有点儿明白这一点。 胡锦涛明白了什么呢？ 那就是U邓小平之后的中国政局，有一张最大的王牌，谁有能耐把它打出来，谁就可以翻天覆地，领一代风骚。 这张最大王牌，就是“六四”平反。<br />
最近一段时间，有人不时放风，说胡锦涛、温家宝正在审慎地考虑重新评价“六四”的问题。 中国佛教协会换个届，就有人在那里张扬说U这次就任的中国佛教协会会长传印长老，在一九八九年上街游行过，是支持学生的，而更有一个新任副会长，当年甚至曾经公开抗议军事镇压，这都是当局在传递要重新评价“六四”的信号。 温家宝“再回兴义忆耀邦”的文章一出笼，有人也说这预示着胡温领导层在时机成熟的时候会重评“六四”。 据说，在海外中文网站上，有化名人士发表看法，更是直截了当地把重新评价“六四”与胡锦涛十八大续任总书记联系到一起，意思不外是说，一旦胡锦涛十八大有机会干第三届，他就会重新评价“六四”，云云。<br />
