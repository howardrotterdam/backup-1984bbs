																											<div id="postmessage_706298" class="t_msgfont">未来两年半中南海政局大概<br />
<br />
王功谠U未来两年半中南海政局大概<br />
<br />
――看他如何打造胡氏王朝<br />
<br />
作者: 王功谠打印此文&nbsp; &nbsp;告诉好友<br />
2010-06-25 3:00 AM<br />
<br />
<br />
<br />
图为胡锦涛与习近平（右）<br />
<br />
瞎糊弄，官方用语叫“不折腾”<br />
<br />
舆论一般认为胡锦涛是个没有什么作为的平庸政客，甚至还远远不如江泽民那两下子――毕竟，在“六四”之后那么困难的局面下，人家江泽民不仅稳住了共产党的政权，而且还带领中国登上了世界舞台。 当然，对于很多人来说，江泽民的形象不怎么样，尽管他比胡锦涛风雅（或者说是喜欢卖弄风雅）；江泽民的政绩更是极有争议，因为他把中国推入了贫富分化这个深渊― ―还不说其他方面的严重问题。 按道理，有这么一个极具争议的前任，面对他留下的既有经济力量增强、又有突出社会弊病的政治遗产，下任要树立自己的良好形象是比较容易的，要有所作为也不是很困难的。 事实上，在胡锦涛接任伊始，他就占了这个便宜。 受够了江泽民十几年那小丑般的表演，看到一张人皮面具就觉得满纯朴、满可爱的；完全虚空的“新政”两个字，就能让中国人说服自己对着这张人皮面具山呼外岁。 结果呢，胡锦涛登基如今已经整整七年半过去了，还有几个具备常识的人仍然对他抱有希望？ 应该是爱得越深失望越大的缘故吧，舆论现在反过头来都说他无能，纷纷预言他这剩下的两年半任期注定是得过且过的瞎糊弄――官方用语叫“不折腾”。<br />
不过，这种说法只是看准了一半。 如何治国，胡锦涛肯定是没有什么作为的；但是，如何固权，胡锦涛其实正准备在今后的这两年半里大展拳脚。 记得十多年前，吴国光教授在香港撰文评说江泽民，曾经把政治家分为三类U一类是会搞权也懂治国，那是一流的政治家，比如邓小平；二类是会治国不善固权，胡耀邦、赵紫阳都属于这种类型；三类是揽权在行而治国不行，那就是江泽民这样的了。 这个说法传到北京，一时大家争相转述，听说也很惹得江泽民发了脾气。 我们现在套这个说法来看胡锦涛，恐怕要说胡也是三类政治家的优秀人才U国家事，随他娘，老子最感兴趣也最擅长的就是搞权！<br />
<br />
胡锦涛总感权力不够<br />
<br />
说胡锦涛擅长搞权，不少人可能不同意，不过这只需要一个论据就可以说服他们U胡在“储君”位子上整整十年，耐得寒窑，受得委屈，装得起孙子卖得了傻，为的是什么？ 还不就是为的登上权位？ 胡耀邦、赵紫阳当年的位子比他等待接班时候的处境显赫多了，但是，却没有最终成为名副其实的第一把手，而胡锦涛比胡耀邦、赵紫阳都强――强就强权力游戏上U胡耀邦、赵紫阳是权力游戏的输家，而胡锦涛是赢家。 说胡锦涛最感兴趣的就是权力，这话也会有不少人不同意，这也只需要一个论据就可以说服他们U如果他不是对权力有超级兴趣，怎么可能装孙子装上十年来等待达到最高权力的那一天？ 你说他是为了拿到权力好做事情？ 人家胡耀邦、赵紫阳不是真正的第一把手的时候，并没有在那里等待当了实权第一把手再来做事，而是实实在在地做了很多事，很多为人民、为国家的好事，甚至为了做这些事而最终牺牲了自己的权位。 对比之下，胡锦涛的不同就很明显了。 再说，胡锦涛当第一把手已经七年半了，他为国为民做了什么像样的事情？<br />
胡锦涛不做正经事的一个理由，说是因为江泽民在那里垂帘听政。 其实这个借口不能成立U邓小平垂帘听政比江泽民厉害，在那个形势下胡耀邦、赵紫阳照样为国为民做事。 当然，胡耀邦、赵紫阳因此下了台，胡锦涛不想走这条路子――这不正是咱们这里所说的嘛U胡锦涛第一在乎的是他的权位。 天下的事情，偏偏就是这样奇怪U越是在乎权力的人，就越发地、总是地感觉自己的权力不够。 毛泽东在文化大革命的高潮年代还发牢骚说他自己的权力不超过北京周围几十里呢。 那些要等权力够了才能做事情的人，一辈子捞权都忙不完的，到死也不会感觉到权力足够做事的了。 胡锦涛的政治生命眼看就要走到了尽头，他的逻辑也是一样的U越到后来，越要拼命地抓权，最好留下一个胡氏王朝的政治基业，下台之后也能把权抓在手里。 这可以说是前面所说的第三类政治家的共性，江泽民、胡锦涛一丘之貉。<br />
<br />
螟蛉子江泽民对遗腹子胡锦涛<br />
<br />
一丘之貉之间，争得就是权力。 胡锦涛在任七年半来，最大的苦恼就是老觉得自己的权力不够，甚至权位不稳。 从另一方面来说，权力不够也是真的，因为他的前任江泽民给他留下的权力遗产就是江家帮、上海帮、太子当充斥中央领导层。 至于说权位不稳，那却有点儿自己吓唬自己，老江根本就没有胆子敢把小胡搞下来。 这个力量，按说老江是有的。 十六届十一个政治局常委，江的嫡系占了八个；十七届也有至少六个，都是绝对多数。 还不要说老江在军队中多年苦心经营下的人马。 可是， 老江不是老邓，就在这里。 老江没有这个恶胆，十六大上只能眼睁睁地让位给胡锦涛，不敢把他踢下去；小胡接位之后，再把他拉下来，那就更是大逆不道了，老江恐怕连想也没敢想过。 这两个方面，权位甚稳和权力不够，其实是两个相辅相成的方面U老江因为没有胆量把小胡搞下去，于是千方百计用自己的人马来钳制小胡。 到了十七大，还替他选一个接班人，终于过过当“邓小平第二“的瘾头。 按照这个路子走来，在这样走下去，可以说，自从邓小平变成植物人躺在三零一医院等死的那天起，中国就进入江氏王朝了。 胡锦涛不过是临时给人家看家十年，有点像新加坡的吴作栋，从李光耀手里接过位子，然后传给李光耀的儿子李显龙，而这个过程中李光耀一直都还是内阁资政，垂不垂帘听不听政都无所谓的，反正你不能走了我的样儿。<br />
吴作栋对李光耀只有感激的份儿，因为他是李光耀拉拔起来的；胡锦涛人家那可是邓小平选的接班人，就此而言他比江泽民还牛，因为江泽民是“六四”那个时候李先念推荐的人选，老邓因为自己选的两个接班人（胡耀邦、赵紫阳）都不成而没有办法不得不接受了这个人选。 这就是说，在政治上，江泽民是邓小平的过继儿子，就像他在家族中是江上清的过继儿子一样；而胡锦涛是邓小平在政治上的亲生儿子，就像邓朴方在血缘上是邓小平的亲生儿子一样。 都是邓小平的传人，不过一个是螟蛉子（江泽民），一个是遗腹子（胡锦涛）。 正宗看不起过继的，尽管过继的排行老大。 老江没有贼胆搞掉小胡，原因盖在于此也；而小胡不满自己仅仅为江氏王朝当个十年总管家，还要利用本届任期的剩下两年半，决心打造自己的胡氏王朝，其原因亦盖在于此也。<br />
<br />
欲组胡氏班底？ 难也哉！<br />
<br />
怎么打造胡氏王朝呢？ 简单说来，胡锦涛有上中下三个目标。 他是一个谨慎小心而求稳当的人，因此有下策；但又不甘总是小心翼翼给人家当童养媳，于是还有中策；下中两策其实又都不易达成，那么小胡也许就不得不铤而走险追求最高目标了。 应该记住的是，所有这些，目的都是权利、位置，无关治国路线。 但是，为了争夺权位，治国路线是可以拿来当牌出的。 当王牌出尽的时候，那就顾不得三思了。<br />
今后两年半，胡锦涛心心念念，首先是要实现他的最低目标，那就是在十八大上组成一个胡氏人马占据多数的中央政治局常委会。 政治局这个层面，已经问题不是很大，胡锦涛这几年已经布好了人马，抢占了不少滩头阵地，特别是早先在共青团中央共事的团派大员，包括张宝顺、刘奇葆等等，已经一个个虎视眈眈，要在十八大上进入政治局了。 可是，政治局常委这个层面，却很让人犯愁。 按年龄算，如果维持“七上八下”的原则，也就是六十八岁者退休、六十七岁者留任的话，则预定二0一二年秋天的十八大，就将以一九四四年出生划线。 本届政治局二十五名委员（包括九个常委），十三位划到此线之上，笃定卸任；十一位划到此线之下，应该留任。 还有一位不上不下，出生在一九四四年十二月，按年头算够了六十八岁，但按月份算则还不够六十八岁。 本来嘛，有人拉拔，运气好，就可能留任，而留任则可能进入政治局常委。 此人叫做王乐泉，已经连任两届政治局委员，本来治理新疆有功，作为胡总的团派亲信是有很大可能在十八大上升任常委主管刀把子政法事务的。 可惜他运气不好，去年七月新疆出事了。 这样一来，王乐泉十八大肯定是不可能留任政治局了。 这样一来，就看那十一位一九四五年及此后出生的现任政治局委员们，谁比较有可能当下届常委了。<br />
习近平、李克强已经进入常委，当然会连任，余下还有九人。 这九个人当中，有三个已经连任两届政治局委员，属于资深人士，应该具有进入常委的优先优势。 他们是U 刘云山、张德江、俞正声。 刘是团派人马，张是江泽民亲信，俞是正经太子党。 接下来，有两个现任政治局委员目前具备位置上的优势，十八大争常委的实力雄厚。 一个是李源潮，团派，进入常委主管书记处的可能极高；另一个是王岐山，太子党，据说有争下任总理的雄心，至少当国务院常务副总理并巡此进入政治局常委是轻而易举的。 以上七个人，只有李克强、刘云山、李源潮三个是胡锦涛人马。 三对四，这样的政治局常委会组成决不符合胡锦涛的心意。<br />
还有四个现任政治局委员，他们正在激烈地竞争十八大的常委位置。 这四个人按年龄排序是U刘延东、张高丽、薄熙来、汪洋。 按派系背景看，这四个人则是一个半胡锦涛人马对两个半江泽民人马U汪洋是胡锦涛的安徽老乡兼团派亲信；张、薄分属广东帮和太子党，都是江泽民系统的人马；刘延东当然是团派，但她的父亲刘瑞龙是江泽民养父江上清的亲密战友，所以她是江胡左右逢源。 除了汪洋，其他三个人到十九大就都退休了，故十八大是他们进入常委的最后机会。 然而，就算汪洋、刘延东两个一起进入下届九人常委会，胡锦涛也不过掌握四票半，刚刚与江泽民人马平分秋色。<br />
于是京城传言令计划十八大要进常委。 胡总当然很想这样做，可是，挤掉谁呢？ 说实话，想挤掉谁都难。 除非胡锦涛有本领进一步降低政治局委员的退休年龄，比如说以六十六岁划线，那就可以把俞正声、张德江、张高丽等三员非胡派大员挤掉，外搭一个刘延东。 上述人等剩下七个，全当常委，“胡江比”将是四比三。 最近总有人说下届常委可能改成七名，大约和这个有关系吧？ 问题是，改划年龄线，这个难度并不低。 眼看着自己的人马被这样整掉，江泽民、曾庆红难道就能同意？ 干了一辈子刚要有了“攀上顶峰“（温家宝语）的机会的俞、张、张们，难道就会束手就擒？<br />
