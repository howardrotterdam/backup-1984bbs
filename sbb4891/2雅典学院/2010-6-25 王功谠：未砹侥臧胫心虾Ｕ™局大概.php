“爹有娘有，不如自有”<br />
<br />
这是北方的一句俗话，意思应该很容易明白U老爸、老娘再富有，毕竟不如自己本人富有。 现在都是独生子女，“啃老族”，也许这句话不管用了。 可是，过去那些富家子弟，一心吃喝嫖赌，银元却被老爹、老娘攥着，自己手头就很难受了。 放到政治上，其实也是一样U哪怕自己的亲爹当皇帝，那也远远不如自己来作。 李世民发动“玄武门之变”，杀的是大哥李建成、三弟李元吉，可直接的目的却是要逼迫老爹李渊把皇帝位子让出来，自己黄袍加身。<br />
考诸十八大之前的中国政局，并没有什么李渊、李世民。 这里只有江泽民、胡锦涛、习近平、李克强，好像是一部四方演义，实际上当然是两军对垒U江泽民、习近平是一条线，胡锦涛、李克强是另一条线。 我们在本文的前两部分已经谈过，一旦习近平在十八大上接班，出任中共总书记，那就等于胡锦涛为江泽民当了十年的大管家，最终不过是江氏王朝权力传承的一个“二传手”。 这个大管家和“二传手”一旦卸任，前有太上太皇江泽民，后有老江亲手选定的接班人习近平，恐怕日子很不好过。 这十年间，胡锦涛不得不敲打敲打江泽民的人马，否则他几乎干不下去。 十八大之后，特别是胡锦涛在十八届三中或四中全会交卸军委主席之后，老江会不会报一箭之仇，我们不知道。 谁敢说老江不是那样小心眼的人？ 习近平要真正立威， 唯一的选择就是拿胡系人马开铡，抓他一个陈希同第三、陈良宇第二。 但凡属于胡锦涛亲信人马的十八届政治局委员（不包括政治局常委，他们有政治保险；至于小小的中央委员们则不够格，一把杀他三五个也不足以为新君立威） ，都要小心了。 与胡关系越紧，功劳政绩越大，反而就越是危险。 君不见，陈希同一九八九年天安门杀伐，那为老邓立下了何等的功劳？ 陈良宇为“上海帮”看守老家，据说还很有希望成为隔代接班人的，那是得到了老江何等的信任？ 汪洋、刘延东、张宝顺、刘奇葆、郭金龙、胡春华，哪个会被习近平选中祭刀？ 那还真得看他们的本事了。 稍微聪明一点的，马上未见风就转舵，借了胡锦涛的大腿刚刚入局，政治局会议室的位子还没找到，恐怕就得赶快向习近平磕头输诚。 这样一来， 就算胡锦涛有本领能在十八届政治局及其常委会里满满塞进自己的喽罗，又怎能禁得住这些喽罗们在习近平的龙铡前哆嗦？<br />
所以，我们说，胡锦涛最好的选择，还是把习近平一脚踢掉，让李克强接掌大位，打造胡氏王朝来取代江氏王朝。 问题是，这说来容易，做来太难，那岂止是和“上海帮”、“太子党”一把撕破脸皮，根本就是一场政变。 当然，胡锦涛已经在多方笼络王岐山，不是没有可能退让一步，在李克强接任总书记的同时，给“太子党”留出总理职位，让前副总理姚依林的女婿王岐山，给前北大学生会主席当个财经事务总管，也算是个好的政治平衡。 习近平的出路，据说就是个“阳春”国家主席――阳春面者，素面也，什么佐料都没有，就是一碗白面条。 当然有助消化，可惜不能壮阳。 你想这江泽民、曾庆红、习近平们能答应吗？ 你胡锦涛还显摆自己是胡耀邦的传人，可人家习近平的老爹习仲勋曾经为胡耀邦两肋插刀，你居然下他习近平的毒手！ 这还不说八十年前陕北救中央的旧事，为此连毛老头子都有三分忌惮习仲勋咧。 你可以说，习近平何德何能，居然接班；人家只消反手同样一问U你的李克强又何德何能，可以取习近平而代之？<br />
安插亲信，难；而且，即使办成了，也很可能并不管用。 踢掉习近平，更难，打不着黄鼠狼反赚一身骚还是好的；搞不好，被人家反制过来，整个“团派”鸡飞蛋打，胡锦涛下场也不会太妙。 怎么办？ 怎么办？<br />
这就可以回到今天开头那句话了U胡锦涛还有更大的妙计，那就是干脆自己再干它一届！ 自己这个皇帝，继续作下去，那岂不是天下最美妙的事情？<br />
