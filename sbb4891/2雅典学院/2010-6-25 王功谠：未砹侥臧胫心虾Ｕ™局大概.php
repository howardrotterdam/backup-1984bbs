<br />
储君既定，你敢换他？<br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;其实，就算胡锦涛最后能够绞尽脑汁地实现他在十八大和十九大两届政治局常委掌握多数人马的目标，那也不过是把当年江泽民的把戏重演一遍。 江泽民当年那么干，其实有他的苦衷。 你想想，集党政军最高职务于一身、在第一号领导人的位子上坐了十好几年的这么一位，临到退位，接班的总书记和总理居然没有一个是他的门生或亲信，这叫什么说道？ 我并不是同情江泽民，可是这个制度有这个制度的逻辑U不是你的亲信，但却接你的班，下一步往往就是清算前任以树立威望和打造政绩。 以毛泽东那样的一世枭雄，因为选了个接班人无德无能，还不是落得身后家破妻离、人亡政息的局面？ 说华国锋无德，那是因为它接毛的班但却抓了毛的老婆；说他无能，那就是被邓小平轻而易举地篡党夺权了。 江泽民在十六大那个时候退休的当口，心里还不明白这个？ 何况，江泽民的屁股上，屎还少吗？ 这接班的人要拿他开刀祭权，他能有什么办法？ 所以，只好绞尽脑汁地把十六大和十七大的两届政治局常委都安排是自己的亲信占据多数，那也可以说是不得已也。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;搞成这个局面，问题的关键还在于U江泽民当年不敢把胡锦涛搞掉，不敢不让他接班。 其实，就算江泽民是过继的，胡锦涛是亲生的，可是，那个时候，一九九七年老邓伸腿之后的那五年里，如果江泽民咬咬牙、狠狠心，就把小胡的储君地位给他端了，恐怕也没有多大风浪。 江泽民他居然就是不敢这么干。 是江泽民仁心醇厚？ 鬼才相信这个。 是邓小平死前有什么密诏？ 这个我们是猜不到的。 反正结局就是那样U新主接班，但居于弱势；旧主让位，但掌控政治局常委的多数。 我们前面强调过，说胡锦涛接班之后还是没有安全感，总觉得自己位置不稳，权力不够。 其实事情都可以两面看U难道下了台的江泽民反而就有更多的安全感不成？ 下台之后，大位让人家做了，不知道什么时候也可能就来收拾自己，或者收拾自己的儿子，他江泽民也一样没有安全感！<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;重复一遍U毛病就出在这个储君身上。 不是说出在当年的胡锦涛身上，而是说出在邓小平为江泽民选择了胡锦涛这样一套搞法上面，出在胡锦涛是邓小平亲生儿子而江泽民是邓小平过继儿子这上面。 现在胡锦涛所面临的困境，几乎与他当年的优势恰成一体两面，也就是与当年江泽民所面临的困境一模一样U前任指定了自己的接班人，自己退位之后就面临很多难以设想的危险，只好想办法在十八大的政治局常委中搞个自己的亲信多数，来作为下台之后的政治保险了。 或者说，就像我们前面所分析过的，胡锦涛目下处境甚至比江泽民当年还要困难，因为他看来很难实现这个目标。 如果实现不了，胡锦涛下台之后的命运，恐怕就比江泽民要差得远了。 你想想，江泽民下台之后尽管仍然握有占据政治局常委会多数的那几位亲信，可是胡锦涛还动不动就把屎盆子望江泽民头上扣呢，但凡事情出了问题就造舆论说是江泽民遗留的（这倒也不全是瞎说），或者是因为江泽民在掣肘（恐怕也一样不全是瞎说），甚至跃跃欲试要整几个老江的人马比如贾庆林、黄菊之流。 胡锦涛末年，中国社会矛盾这么激烈，等新人接的班来，新班子里有没有几个胡的亲信，新主还不拿胡锦涛祭刀来平息社会矛盾呀？ 胡锦涛不是喜欢毛泽东吗？ 恐怕他下台之后的命运也将和老毛死后的命运相差无几！<br />
<br />
谁将是“陈希同第三、陈良宇第二'？<br />
<br />
<br />
以上这些分析，可以说，胡锦涛比谁看得都清楚。 既然已经看明白了，他就当然不会甘心做那亲信被抓、自己名誉被污名的老毛第二，而是要努力改变这个命运。 他甚至也不甘心做江泽民第二，满足于有几个亲信充斥政治局常委多数来当自己下台后的政治保险――原因很简单，胡锦涛自己比谁都明白，江泽民的接班人是如何可能威胁江泽民及其家人、亲信的命运的，胡锦涛自己下台后的命运就如何可能加倍地被后来的接班人所威胁。 你不相信这个“加倍”的说法？ 不妨举一个例子。<br />
江泽民树立自己在邓小平之后的权威的关键一步棋，是抓了陈希同；胡锦涛有样学样，则是抓了陈良宇。 两个陈，都是政治局委员；一个是北京市委书记，仗着自己是天安门镇压的功臣而倨傲不服；一个是上海市委书记，那是前朝天子的龙祥之地的嚣张地头蛇。 这似乎已经形成惯例，以至于胡锦涛要抓陈良宇的时候连江泽民也表示对此理解并确实让了步。 胡锦涛之后呢？ 新人接班之后的两年左右、三年之内，那就一定会抓一个身为政治局委员的胡锦涛嫡系要员。 这个人会是谁呢？ 等十八届政治局委员名单公布之后，我们就可以估摸个大概了。 比如说，汪洋如果进不了十八届政治局常委，他就是一个很好的目标；习近平只要动用贪污腐败案把他拿下，马上权威大树，胡时代就立马转向习时代了。 当然，还有别的选择，张宝顺、刘奇葆、胡春华、周强等等，那就看胡的亲信们谁能在十八大上进入政治局，也就是谁离政治死期不远了。 说到底，习近平不做这件事情还不行，因为前两任江泽民、胡锦涛都已经这么做了，你不这么做，人家就欺负你软蛋。 一天还没做这件事，就一天还不能立威。<br />
可是，再一再二，不可再三。 你这游戏已经玩了足足两轮了，那些身在砧板的政治局大员们难道还没有看清楚这个玩法呀？ 如果看清楚了，那就会“上有政策，下有对策“，他也不会束手让你去抓吧？ 怎么办呢？ 那些本来是胡锦涛最为信任的政治局委员们，完全是因为紧跟胡锦涛才得以进入政治局的委员们，在十八大进入政治局之后，也就是一旦习近平接了班，他们可能就要比任何人都更加殷勤、坚定、努力地向习近平献忠心了。 哪个不献，或者哪个献忠心落于人后了，那个就是陈希同第三、陈良宇第二。 这样一来，游戏的规则就变了U本来你是旧主的人，旧主为的是自己下台后的命运保险而把你拉拔进最高领导层，可是你转脸就投靠新主了。 这很好理解U不投靠就可能被抓呀，这可不是小风险。 问题是U旧主的保险白买了。<br />
所以说，就算他能实现最低目标，胡锦涛下台之后的命运也将远远不如江泽民。 江泽民已经没有很大安全感了，胡锦涛恐怕根本就不安全。 胡锦涛应该也已经看明白了这一点，因为谁也不能低估他在这个方面的老谋深算。 怎么办？ 只好釜底抽薪U那就是拿掉储君，自选接班人。<br />
