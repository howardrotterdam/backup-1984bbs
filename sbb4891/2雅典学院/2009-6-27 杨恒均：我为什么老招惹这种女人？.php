以上只是国内当时能够看到的一段，还有更加严重的，可是我看来看去，所有的起因竟然是“那次演讲杨恒均招呼了一大帮粉丝为他捧场”。至于我如何“烂人”和精神分裂大概都是从这里来的，甚至还有一些更过分的话，如果需要我也请网友转过来。<br />
<br />
可是，我觉得没有什么，因为我们没有任何交往，只有那次演讲匆匆一见，我认为她如果不喜欢我的演讲，说我文章写得水，这是非常正常的，我自己都认为演讲一塌糊涂，文章更是不入大流（所以我才写博客啊，你以为我不想写中国大报纸的专栏？），又太嗦，这种对我的批评，来自不锈钢，我非常欣赏。而且，她也许误会了，认为那些来听我演讲的北京朋友，都是我”招呼过来为我捧场的“。<br />
<br />
可问题是那种不停在各个网络包括笔会内部论坛上对我人格的污蔑：烂人、精神病，人格障碍――我就有些不理解了。演讲不好，文章不好，就是烂人，就是人格障碍？而且，是一个曾经主动去听我演讲的我自认为有相同理念的年轻女子。我好像没有强迫你去听，也没有叫你（虽然，说实话，我还真想那次演讲你能去，让我认识一个奇女子），如果你听了我的演讲，觉得浪费了自己的时间，我这里向你道歉。<br />
<br />
可不久我就发现，你所有对我的评判都来自一个前提：杨恒均招呼了一大帮粉丝为他捧场！<br />
<br />
这一句话因为我一直没有解释，结果，现在几乎连所有认识我的人都这样认为，可是，唯独这一句话是在大家所有批评和攻击我的言论中，是可以证实的！因为很简单，那次演讲，不但没有叫一大帮“粉丝”过去给我捧场，而且，我甚至总共没有发过超过三个信息说我在那里演讲。而且，这根本不是什么粉丝，一个是我原来在北京工作时单位的年轻人，一个是南开大学的教授（我自己的哥们），还有一个我不太记得了，好像做生意的。<br />
<br />
那么，有网友告诉我，你自己的演讲，就真叫读者去捧场，有什麽问题吗？为什么虚伪到不承认？我说，当然没有问题，可问题是，那次我恰巧没有叫任何人去啊，更没有找人给我捧场。那是比较敏感的日子，我很低调。而且，如果我要叫读者去捧场，我会打电话――能够有电话联系的，还是“粉丝”？早就是个人朋友了啊，可那天整个来听我演讲的，总共只有两个熟人，还是我早就熟悉的哥们姐们啊。还有，我如果真想很多人去，大家可以去看一下，我在任何一个日流量上千人次的博客做广告没有？如果做了，那去捧场的人当然不会少。可我做了吗？<br />
<br />
再说，不锈钢你不是也去了我的演讲会？你还是一个早就出名，现在也比我有名的名人，你去我的演讲会，别人也可以去吧。最多是批评和指教嘛，有啥不可？何苦从这件事竟然延伸到我的人品？<br />
<br />
我见的网友和朋友还真很多，由于我的博文议题是政治，比较刚性，这些年我见的网友几乎百分之九十九是思想和身体上的猛男，从他们那里我学到了很多，我很多文章就是“偷”他们的观点。由于我这人始终很随便，有时“为老不尊”，讲话就更是随便，也得罪哥们姐们（例如一个坐车来回整整八小时的网友哥们专门去见我，可我只有一个小时陪他聊天，就不得不让他回去，我心里一直很愧疚，如果他攻击我，我非常理解！）。总有一天，可能有我的朋友和熟识的网友出来批评我，而且我也理解，网络上，我们连强权都可以批评，互相批评一下不是很正常？<br />
<br />
可不正常的是，批评甚至攻击我的网友恰恰是我根本没有见过面，也不那么想交往的一两个”奇“女子。这位突然跳出来的不锈钢，就让我大吃一惊，我竭力思考，在我们短短三句话的交往中，有什么事情让她“第一眼见到我就觉得我很烂，觉得我有人格疾病？” <br />
<br />
当然，这是人家的感觉，也无可非议，可是，当你的感觉是根据一个完全没有的前提（他叫一大群粉丝来为他捧场），而你煞有介事地到处宣传的时候，你是否想到，你在伤害一个甚至没有见过你就尊重你的人？<br />
<br />
所以，我也才觉得有必要找你解释一下，问一下，你也许对我误会了，这是一个简单的事。请问各位网友，你们看到过这个网络上，被伤害的人反而主动找伤害他的人去解释的？<br />
<br />
所以，大家注意看我昨天找不锈钢的那些解释，她看到我第一次在内部论坛讲话，故意不理我（后来告诉别人，她不理我，我故意找她），然后我还没有解释几句，她又来用那些什么人格障碍等侮辱人。<br />
<br />
我就很奇怪，我是来找你解释的啊。你怎么这么态度？莫非你认为你有权利没有根据的攻击你想攻击的人？<br />
<br />
<br />
于是有网友看到我在昨天的发言威胁她，我也要写她，我生气了，可是，如果你知道前应后果，你会怎么做？<br />
<br />
<br />
那么，我是不是告诉你，我也可以这样写你？我也可以写一下我见到你的感觉？你知道那样很伤人，而我是多么不愿意伤你一个小女子，可你正利用这一点，却在那里肆无忌惮。你这叫自由吗？有人看到我后面是威胁，其实正好相反，如果我以自己掌握的网络资源，突然发帖反攻你这样一个女子，而不是在内部论坛先当很多笔友的面打一个招呼，寻求解释，提醒你：你可以没有根据，而且不愿意听解释地攻击我，但别人也可以这样对你？<br />
<br />
<br />
说实话，每天都有无数个攻击我的帖子在各大博客，大家现在去看看，但我感觉到那些人不喜欢我的文章，大家可以商量，或者他们感觉到没有商量的余地，就辱骂我。我不知道他们是谁，而且我认为，如果连他们是谁都不知道，就更没有必要回击了，后来我连看都不看了。可你不锈钢可是京城名人，秦城一派，据说有很高的威信。我虽然一早对你的帖子也是抱住看都不看的态度 ，可是你不但没有停下来，还到笔会这种作家云集的地方去，在我不在场的地方，指责，辱骂？――你确实让我想不通，我上网也就是管自己的一亩三分地的博客。我真不知道，哪里让你如此仇视我？听说你一直是宣扬先进理念的，可是，你在帖子里竟然说”我垄断了民主“，天啊，我就是一个再草根不过的博客写作者，一个写日记的啊。我求你和一些人，讨厌我可以，千万别到我博客来看了，好嘛？难道我一定要停止，才不会让自己很水的文章阻碍了你对人生幸福的追求？<br />
<br />
还有一些网友用“名人”来说事，我不能理解，我想在这里提醒所有认识我的网友，特别是见过面，或者聊过天的，我无话不说，随便得让很多网友难堪，请问，我这个写博客的，和名人有什么G8关系？我以前是草根，今后也是草根。如果真要说名人， 这不锈钢好像是个名人，我在没有使用杨恒均笔名写文章前，就听说过她的名字。<br />
<br />
昨天在和她的公开聊天中，我确实觉得很生气，甚至一度不理智，而且看到一个侮辱我的人，在我请求解释的时候，竟然当着所有笔友的面继续侮辱我”人格缺陷“，所以，我告诉她，如果你真用这种下三烂的手段对付我，一点不讲道理，别以为我就不会还击人，我会停下写正经文章一天，陪你玩。<br />
<br />
<br />
可是那样，我不但会伤害你，而且，很多人会出来说，你不风度，你不男人，你性格偏狭，这就奇怪了，一个我不认识的人到处说我”很烂“有精神病，就可以，而我出来说，就有问题了？我曾经写过一些回击一些网友的文章，说得都是事实，可是，由于我天生语言有点”犀利“，有人说太过激了，于是我拿了下来，于是我只好继续忍受别人如何如何说。可是，我能不能求大家：说我生活，或者搞民主是为了女人的，求你拿一个活生生的我认识的，或者在街道上那怕多看了一眼的女人，来向我证明，好不好啊？指责我生活有问题的人，从来没有见过我，指责我烂人一个，有精神病的人，竟然最多说过三句话？这世道还真有意思啊。。。<br />
<br />
<br />
但是，想来想去，其实也有让我值得高兴的，因为迄今为止，在这个世界上，还真没有出现过一个熟悉的网友对我产生误会。如果你真认识我，你会知道，我就是和到你们家收垃圾的人没有区别，即便我真的”很烂，甚至性格有问题“，但这好像一个人的胖瘦，一个人的美丑一样，你那样不直接找我，却到处说，很不对，知道吗？<br />
<br />
就像我如果现在开始写文章，用文学的语言描述一个奇丑无比的女人，一个让我想吐的女人一样，我很痛快，但我下不了手啊！我虽然是反对派，以批评为己任，但我还有自己的标准。我每一天都在批评一些强大的东西，可是每一次批评中涉及到个人，我都很小心。<br />
<br />
我想，我以和你父辈一样的年纪，只是想告诉你，应该把有根据的批评，以及对任何你不熟悉人发表评价的自由和那种侮辱一个你不认识的人的人格区别开来！<br />
<br />
另外，有网友发来信息，质疑我是不是和这两位女子串通好来炒作的，我这里解释一下，虽然，明眼人也看出来了，这确实很象炒作，因为，从我这边来看，由于没有任何实质伤害（事实都没有），而每一次事件过后，就有大批人开始看我的文章，想接触我这个人。――但是，我发誓，我没有一点炒作的意思。我也想借机对认识和不认识我的朋友说一句：认识我，一，可以和我亲自接触，有时间哥们姐们喝茶聊天，二，可以通过我的博客。至于在这两点之前，先别下结论啊。</div>

							
							
							
							
