于是这些年下来，我痛改前非，一直严格要求自己。可是，我是有过去的，而且这些过去普通网友也许不清楚，可某些人士特别是某些维护利益集团的重要人士知道呀，他们如果跳出来，或者用匿名的方式，不是随时可以“搞臭”我？<br />
