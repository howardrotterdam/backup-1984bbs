然而，这些年过去，没有人这样搞我，我曾经问一位老战友为什么，他说，我们也许像你所说的被利益蒙骗了，从而分不清什么是好的什么是坏的，但我们知道什么是真的什么是假的，你如果真诚地去追求你认为是正义的东西，没有人可以真正动你，或者动摇你！<br />
<br />
我想告诉你，这就是人品，小昭。所以当网络上所有批评我的人，哪怕竭尽侮辱之能事，我都能一笑置之，可是，看到你的博客竟然指责我把民主作为工具找“颜如玉”的时候，我目瞪口呆。因为，你的文章中以说真相为由，把我描绘成一个利用民主骗某个老姑娘感情的色情高手！我自己说到这里都觉得有些恶心！<br />
<br />
这也是为什么我不能不回应你。我最后只想告诉你一句，这是我唯一写文章回应网络上对我的不实批评和指责，算是我对你多次写我、介绍我、推荐我作品的一点回报。并声明：我言尽于此，不会再对任何文字做回应。任何人甚至可以编造一些信件和对话，把一些莫须有的文字打上引号弄出来，但请记住一些真相：我们没有见过面；你在网络上找到我（而不是我找到你！），并在自愿的情况下（没有武力威胁、感情要挟和金钱引诱）帮我开设和打理博客，并愿意接受我的要求，因为是我的名字的博客；我们也许在网络上谈过感情，甚至打情骂俏过，但你是三十多岁的妇女，有独立人格；你从来不是我的女朋友；你我不通话和不通信已经有好几个月；我没有用民主去追求或者胁迫你这个“颜如玉”，更没有用民主去追求其他的任何女人，今后也不会！<br />
