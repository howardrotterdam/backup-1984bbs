													<h2>张志新曾经是一名美丽的女人</h2>
						
						
																											<div id="postmessage_600519" class="t_msgfont">　　<br />
<br />
　　苏小和<br />
<br />
　　张志新死于1975年。她曾经是一名美丽的女人，多年以后我们还能在照片上看到她象牙一般的皮肤，她斜靠在小提琴上的线条温柔的脸。可是在1975年，她被枪毙了，被拉出去之前，张志新曾经拼命叫喊，那些行刑的人找来一把快刀，将她的喉管切开。张志新最终只能发出一连串混沌的声音，年轻的血液顺着气管冒出一连串气泡，咕隆咕隆的，张志新自己也没有听得太清楚。<br />
<br />
　　事实上，张志新之死从1968年春天一个寻常的星期天就已经展开。那时，她还是辽宁省委宣传部的一名干部，这一天，张志新到一位姓阎的女同事家借江青的文革讲话资料，两个人之间一段私人化的交流让她的命运突转。<br />
<br />
　　像所有的革命战友一样，这位女同事劝张志新站到革命派一边，可是张志新却说，“我考虑的不是这一派那一派的问题，我考虑的是文化大革命的问题，我觉得文化大革命好多问题我不能理解。”<br />
<br />
　　说者无意，听者有心。这句话很快被同事汇报上去，记入了张志新的档案。这一年的10月，张志新被下放到盘锦五七干校学习，很快她被揪出来，组织为张志新成立了专案组，对她的思想进行反复深入挖掘。经过一系列批斗会和学习班，张志新逐渐讲出内心的真实想法，提出对文革的诸多怀疑和不满：<br />
<br />
　　“主席讲话中说，文化大革命成绩是主要的，我看不见得。我就想，文化大革命的一些做法，是否非得那样做？群众中刚出现戴忠字时，我就有想法。过去封建时代讲忠，现在搞这个干什么？整这玩艺干什么?再过几十年的人看我们现在和领袖的关系，就象我们现在看从前的人信神信鬼一样不可理解。”<br />
<br />
　　“文化大革命这么弄下去，就剩几个样板戏，唱唱语录歌，祖国的艺术不是越来越枯竭和单调了吗？”<br />
<br />
　　张志新不但怀疑文革的正确性，甚至还将批评的矛头指向国家最高领导人：<br />
<br />
　　“毛主席在党的历史发展中的丰功伟绩是不容否定的。但我认为，在社会主义革命和社会主义建设阶段中，毛主席也有错误。集中表现于大跃进以来，不能遵照客观规律，在一些问题上超越了客观条件和可能，只强调了不断革命论，而忽视了革命发展进段论，使得革命和建设出现了问题、缺点和错误。集中反映在三年困难时期的一些问题上，也就是三面红旗的问题上。”<br />
<br />
　　“对毛主席的怀疑，不信任，归根到底我有那个想法：现在什么问题，都得毛主席说了才好办。毛主席是不是个人说了算太重了。我认为，无论谁都不能例外，不能把个人凌驾于党之上。这些年，什么都提毛主席，放在党的上边。我不反对宣传领袖，但不要宣传得太过分了。”<br />
<br />
　　“林副主席说，主席的话一句顶一万句。主席的指示理解的要执行，不理解的也要执行，这种局面不会维持长久了，这样下去，局面是不堪设想的。”<br />
<br />
　　多年之后我们再来阅读张志新的这些话语，会觉得她仅仅讲述了一些常识，可是在当时，在一个集体盲从的时代，任何一种怀疑文化大革命、怀疑最高领袖的言语或者行动，都足以招致灭顶之灾。所以，张志新留给历史的价值不在于她的理论深度，而是一种勇气，一种对常识的坚守。她的确没有深刻的思考或崭新的创见，但是她不甘受到思想的奴役，并且选择如实将她的所见所感讲述出来。<br />
<br />
　　我们注意到了另外一种事实：尽管张志新对党在文革中的做法提出许多批评意见，但这些意见都是以一名共产党员的身份向组织提出来的。在多次谈话中，她一再表明坚信党的领导，服从组织安排。她记得《党章》中明确赋予党员的批评建议权利：<br />
<br />
　　“党员有权向党的组织和各级领导人提出批评和建议。党员对于党组织的决议、指示，如果有不同的意见，允许保留，并有权越级直至向中央和中央主席报告。要造成一个又有集中又有民主，又有纪律又有自由，又有统一意志又有个人心情舒畅、生动活泼的政治局面。”<br />
<br />
　　张志新显然是个信守党章的人，她相信这样的条款是一名党员的最高制度。因此，她信任党组织会接纳党员的批评，也敢于向组织提出这样的意见：“全国共诛之和党内的民主、个人心情舒畅有矛盾” 。即使面对牢狱和酷刑，她仍坚持说，“前提和目的只有一个，I卫党的原则和革命利益。”<br />
<br />
　　以今天的眼光看，张志新简直就是党的体系中最鲜活、最有建设性的力量之一，她从来就不是异端分子，从未试图突破共产党的思想框架。她本没有离经叛道，只是她的几句真话触动了最高权威脆弱的神经，使得某些人因惶恐而生痛恨，生杀予夺自然不在话下。她当然也无法理解，在高度极权的社会中，一切制度不过是无条件服膺最高权力意志的装饰品，她的生存权利、言论权利随时可能被剥夺。<br />
<br />
　　我们甚至可以说，张志新在某种无意识的状态下，让自己偶然做了组织的祭品、时代的祭品。这正是张志新之死最荒谬的地方：一名坚持党性的党员因为向党组织提出意见，竟然被她衷心热爱的组织关进牢狱，一关就是六年，直到死亡来临。<br />
<br />
　　这是最黑暗的六年时间，张志新一直被关在一间小号里，没有窗户，没有光线，戴着手铐和脚镣，只能坐着，不能躺下。她被彻底隔离起来，她的家人再也没有见过她一面。狱警总是唆使一些刑事犯殴打她，他们揪光了她额前的头发。他们强迫张志新把擦地板的破布塞进嘴巴里，还给她的饭里掺进煤渣。看守所长甚至指示手下不发给张志新必要的女性手纸，后来，一位监狱里不知姓名的妇女告诉张志新的家人，说张志新多次遭到了狱警和男犯人的轮奸。在长期虐待下，张志新精神失常了。她经常赤裸着身子窝在小号里，用窝窝头沾着月经血吃，还在小床上大小便。<br />
<br />
　　即使疯了，张志新也没有苟活的权利。对于张志新“是否精神失常”的问题，监狱工作人员曾向上级请示，但上级未经任何司法调查就做出批示：“她的假象，本质不变，仍按省委批示执行。”对于张志新的最后处理，辽宁省革委负责人毛远新在常委会上说“多活一天多搞一天反革命，杀了算了”。<br />
<br />
　　张志新的死刑可能是迄今为止人类杀人仪式中最为残暴的一章。将一个精神失常的人执行死刑，已经有悖于人道，而在张志新临刑前，为了防止她喊出“反动口号”，监狱人员居然强行割断她的喉管。这是1975年的4月4日，“她被秘密带到监狱的一个二层楼的办公室里。没等张志新弄清是怎么回事，就被几个窜上来的人按倒在水泥地上，头枕一块砖，强行切断了喉管。为了维持呼吸，就把一个三寸来长的小手指粗的不锈钢管插进气管里，再用线将连接着钢管的金属片缝在刀口两边的肉上。张志新奋力反抗，剧痛使她咬断舌尖，血水淌满了前胸。……她脖颈上的伤口，不时地冒着带血的气泡，嘴里不时地呕吐着似血似水的唾液……”<br />
<br />
　　面对如此骇人的杀人场景，愤怒是没有意义的。让我匪夷所思的是，一个看上去庞大到极致的组织，为什么会对一个单纯的女人如此惧怕？他们不仅要消灭掉张志新的肉体，还要剥夺她最后一次正常呼吸与叫喊的权利；他们不仅害怕张志新自己独立思考，还担心她的孩子们的血液中残留着张志新批评与怀疑的火种。<br />
<br />
　　我们应该把1975年定为张志新之年。开春不久，张志新即将被执行死刑之时，党和政府命令张志新的丈夫曾真带着女儿曾林林、儿子曾彤彤一起参加沈阳法院举办的“死囚家属学习班”。在法院讯问下，17岁的曾林林、8岁的曾彤彤做出了这样的笔录：<br />
<br />
　　“曾林林：刚听说张志新犯了反革命的罪行，我当时感觉会影响我进步的。这下可完了。但经过学习提高了认识，母女关系是有阶级性的，她虽然生了我，是我的母亲，可她是反革命，就不是母亲了，已是我的敌人了。她反党反毛主席，我们就和她斗争到底。我后来经过学校老师和家长的教育，我已认识到她反革命，我和她划清限界，并不会影响我的进步。<br />
<br />
　　问：张志新实属死心塌地，罪大恶极，你们有什么想法，看法？<br />
<br />
　　曾林林、曾彤彤：坚决镇压，把她处死刑，为人民除害。我们连尸体也不要，政府愿意怎么处理就怎么处理。我们都拥护。对于张志新在监狱的还有什么财物，我们什么都不要，这有(由)政府处理。”<br />
<br />
　　不过，他们没有想到的是，张志新死后还会继续给他们带来困扰。仅仅过了4年，1979年3月26日，当年下令割断张志新喉管、执行枪决的辽宁省委就不得不作出为张志新平反昭雪，追认她为革命烈士的决定。<br />
<br />
　　这分《决定》写得如此冠冕堂皇，如此义愤填膺：“张志新同志惨遭杀害，是林彪、‘四人帮’及其死党一伙阴谋篡党夺权，推行极左路线，搞法西斯专政所造成的严重恶果。我们要把千仇万恨记在林彪、‘四人帮’及其死党一伙身上。”<br />
<br />
　　那些怀念张志新的人们似乎从这样的文件中读到了希望，但三个月之后，有人忽然意识到张志新的遭遇有可能让更多的人们对这个制度产生更大的怀疑。所谓“稳定压倒一切”，他们后悔了，不再让人们继续谈论张志新的故事，所有的媒体、所有的历史教材开始屏蔽张志新，张志新的名字再次成为一种禁忌。<br />
<br />
　　可以这样说，这是张志新的第二次死亡。她再次从人们的视野中消失。直到今天，张志新的审判案卷仍然在辽宁省高级人民法院作为绝密档案封存。后来出生的那些过于年轻的人们，完全不知道我们的历史里曾经有一个叫做张志新的美丽女人，她的故事，她的身体，她的血液，曾经让多少苦难中的人们夜不能寐，泪水横流。</div>

							
							
							
							
