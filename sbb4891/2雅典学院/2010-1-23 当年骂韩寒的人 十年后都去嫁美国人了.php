晚上没事，把十年前韩寒参加的一期央视《对话》节目翻了出来，十年后再看，仍然很有趣，很有喜感。<br />
这期节目做得相当较劲，编导在较劲，女主持人在较劲，专家在较劲，围观群众也在较劲，他们较劲的对象是个18岁的辍学少年，写了一本书，卖了50万两银子，展露了一点个性，说了几句自信的话，于是，大家都受不了了，觉得这小子狂，要教训他，要引导他走上正路。<br />
<br />
节目里印象最深的是那个辫子姐姐，伶牙俐齿，头脑精明，说实话当时的韩寒比她差多了，整个人透着青涩，说话低着头，声音羞怯，远不如今天的大拿风范，俩人对砍得时候韩寒明显手软。不过辫子姐姐也不是没有疏漏，拿老年王朔的生活积淀来映射18岁韩寒的资历浅薄，实在有点不通，可惜当时韩寒也有点蒙了，一时没察觉出来，光硬挺着说我就是阅历丰富，结果更给人以偏激少年的印象。至于辫子姐姐说我用QQ聊天，你才拿聊天室聊，这话现在看来已经近于幽默了。<br />
<br />
那个女主持人看着也很费劲，节目一开始就笑眯眯对着镜头说，韩寒说啦，我不是那么好对付的，会让你接很多招，你是主持人就要好好应付。然后感觉整个节目中她的对立情绪明显，似乎在可劲的和一个18岁少年较真，就看她笑眯眯的问韩寒：你的书可能明年就没人买了，你的人可能后年就没人理了。整个节目被她调动的刀光剑影，也亏韩寒能硬挺下来。<br />
节目最有趣的部分是把一个叫黄思路的18岁女孩也拉到现场，品学兼优，出书不断，绝对现行教育下的最佳模板，把她往韩寒身边一搁，杀气扑面而来。你韩寒以为写本破书就拉风了，少得瑟，看看真正的腕是什么模样吧。我心说，丫的，整个一出武侠狗血剧嘛，这是江小鱼与花无缺的高中版？不过问题是，他们俩好像都是“绝代双骄”吧。顺手百度了一下，搜了搜十年前红遍大江南北的主旋律女孩，刘亦婷，马楠，黄思路的近况，有一篇文章说，刘亦婷拿了美国绿卡，没有考上哈佛商学院，嫁了一个美国白人，在一个基金做副总裁。马楠在克林顿来访时“代表”中国青年表达过对美国的不满，之后却嫁给了一位美国白人，过起了相夫教子的生活。黄思路北大毕业后到哥伦比亚大学读硕士，同时就职于纽约百老汇尼德兰德演出公司。嗯，思路妹妹过得不错，韩寒就不用惦记了。<br />
<br />
