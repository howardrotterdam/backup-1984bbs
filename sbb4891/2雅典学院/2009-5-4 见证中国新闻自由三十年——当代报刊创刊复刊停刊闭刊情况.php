													<h2>见证中国新闻自由三十年――当代报刊创刊复刊停刊闭刊情况</h2>
						
						
																											<div id="postmessage_86257" class="t_msgfont">《当代报刊创刊复刊停刊闭刊情况》<br />
( 2009年5月3日 10:03 )<br />
标签：杂谈&nbsp;&nbsp;<br />
（根据有关资料整理，以后将不断补充。也欢迎博友提供信息）<br />
<br />
1978年<br />
<br />
&nbsp; &nbsp; 9月11日，《中国青年》杂志复刊。<br />
<br />
&nbsp; &nbsp; 10月11日，贵州诗人黄翔等人创办民间刊物《启蒙》。在创刊号上，黄翔发表了长诗《火神交响曲》。<br />
<br />
&nbsp; &nbsp; 12月22日，北岛、芒克、黄锐、陆焕兴等人创办了文学刊物《今天》。从第二期起，《今天》开始走向正轨：基本保证定期出版，并通过邮寄发行到全国。每期一千本，并先后出版了四本丛书。1980年9月，《今天》得到第一次警告，要求停止一切出版活动。北岛他们改头换面，成立“今天文学研究会”，出版了三期内部资料。同年12月，再次接到最后通谍，被迫停刊。<br />
<br />
&nbsp; &nbsp; 12月中旬，《民主墙》杂志创刊。<br />
<br />
&nbsp; &nbsp; 12月16日，《四五论坛》问世。<br />
<br />
&nbsp; &nbsp; 月底，《今天》和《群众参考消息》创刊。<br />
<br />
1979年<br />
<br />
&nbsp; &nbsp; 1月创刊的民间杂志有《北京之春》等。并出现了七大民刊：《今天》（负责人：北岛、芒克）、《四五论坛》（负责人：徐文立、刘青）、《探索》（负责人：魏京生、杨光、路林）、《人权同盟》（负责人：任畹町、陈旅）、《沃土》（负责人：胡平）、《北京之春》（负责人：王军涛、陈子明、刘迪）、《启蒙》（负责人：贵阳本部，负责人黄翔。从1978年冬到1981年春，从《启蒙》、《民主墙》的出版，到最后一期《今天》杂志，在北京一共涌现出50余种民办杂志，此外还有127种杂志星散于全国至少26个大中城市。这些杂志各具特点，其中一部分是文学杂志，其它杂志或多或少都属于政治类。<br />
<br />
&nbsp; &nbsp; 4月10日，《读书》杂志创刊。创刊号发表了中宣部新闻出版局理论处处长李洪林的《读书无禁区》，引发了激烈争论。<br />
<br />
魏京生还创办了《探索》杂志。<br />
<br />
1980年<br />
<br />
&nbsp; &nbsp; 2月15日，消失了13年之久的《羊城晚报》复刊。<br />
<br />
&nbsp; &nbsp; 6月，曾经办过《文汇报》的钦本立创办了《世界经济导报》，名义隶属上海社科院和中国世界经济学会，实际实行理事会领导下的总编辑负责制。而理事会成员多为经济学界知名学者和退休中共高级官员。该报创办之初的办报方针是：为开放服务，突出自己是对外经济的一个窗口。后来大胆突破禁区，关注政治体制改革，成为著名的“擦边球”媒体。<br />
<br />
&nbsp; &nbsp; 8月，《中国法制报》经过试刊后正式创办。该报于1988年更名为《法制日报》。<br />
<br />
1984年<br />
<br />
&nbsp; &nbsp; 2月11日，《南方周末》创刊，关振东任主编、左方任副主编。首印7000份，但出报的当天全部售完。创刊初期，内容以“三星（体育明星、歌星、影星）为主，形式也以一种娱乐报形式介入市场，这种内容与形式，是对苏联《真理报》模式的一种变革。<br />
<br />
何家栋创办《开拓》文学杂志，任主编，后因发表刘宾雁《第二种忠诚》被查究。<br />
<br />
1985年<br />
<br />
&nbsp; &nbsp; 4月，《南风窗》杂志创刊，以“开启思想之窗，搭沟通之桥，吹清新之风”为理念创刊初期就推出“假如我是广州市长”的活动，让市民为市长提建议，被国际传媒誉为“中国民主化建设的先声”。《南风窗》也迅速成为期刊界的一面旗帜，被誉为“刊林奇葩”。<br />
<br />
1986年<br />
<br />
&nbsp; &nbsp; 第一家民办报纸《中国广告信息报》问世。实际上，《中国广告信息报》也不是完全的民办报纸，它的主管单位是光明日报社，第一任总编辑冯迈是该报总编室的编辑，只是他不再拿报社的工资，不再享受国家的福利劳保，但人事编制仍隶属光明日报社。<br />
<br />
1987年<br />
<br />
&nbsp; &nbsp; 4月17日，由于光远、冯兰瑞等人创办的《经济学周报》创刊，目的是为经济体制改革提供智力支持。1988年改版，由陈子明创办的北京社会与经济研究所接手主办，成为民办报纸。其后大力呼吁推进政治体制改革和新闻自由。<br />
<br />
1988年<br />
<br />
&nbsp; &nbsp; 6月30日，《求是》创刊，《红旗》停刊。<br />
<br />
&nbsp; &nbsp; 王若水、王元化、于浩成共同主持《新启蒙》理论刊物的编辑出版工作。该刊物不定期出版，共出版了四期，另三期稿子已准备好，但由于“六三六五”事件未能继续出版。1989年3月初，在北京都乐书屋举行《新启蒙》发布会，参与者有苏绍智、方励之、王若水、王元化、于浩成等人，于浩成解释为什么叫新启蒙时说：“以前的启蒙反对的是封建主义，现在的启蒙反对的是披着社会主义外衣的封建主义。”<br />
<br />
1989年<br />
<br />
&nbsp; &nbsp; ５月５日北京大学学生自治会主办的学生报纸《新闻导报》在北大出版第二期，每期1000份。<br />
<br />
&nbsp; &nbsp; 7月，《海南纪实》杂志因报道“六三六五”事件停刊。《海南纪实》由韩少功创办于1988年11月，发行时间短、影响却很大，开创“纪实文学”先河。以海南为立足点，刊发当时全国的热点、焦点话题。每篇文章都有深度、力度。刊物图文并茂。一出版就行销全国，发行突破百万份。“六三六五”期间，它是唯一一本以图片的形式报道“六三六五”事件的前后期情况的杂志。1989.6《海南纪实》以图片新闻《天安门观察》报道了六三六五前的天安门广场的形势。1989.7《海南纪实》以图片新闻《戒严部队在北京》报道了六三六五后的天安门广场的形势。<br />
<br />
1990年<br />
<br />
&nbsp; &nbsp; 3月，《书林》出版第三期终刊号。1989年的第6期，《书林》封二一改以往的漫画，而用大黑字录《国际歌》一段：“从来就没有什么救世主，也不靠神仙皇帝。要创造人类的幸福，全靠我们自己。”<br />
<br />
