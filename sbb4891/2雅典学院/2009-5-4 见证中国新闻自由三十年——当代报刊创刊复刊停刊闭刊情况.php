&nbsp; &nbsp; 8月13日，著名“左派”刊物《真理的追求》杂志因为抨击江泽民的“七一讲话”以及“三个代表”的提法，而遭到停刊。此前不久，同是“左派”理论刊物的《中流》杂志也因对江泽民最近有关私人企业家可以入党的讲话提出批评而被停刊。“六三六五”以后创刊的这两个刊物，其办刊宗旨和主要内容，是宣扬坚持所谓正统的马克思主义和毛泽东思想，因而被看成是“左派”的旗帜性刊物。《真理的追求》由中国社会科学院新闻与传播研究所主办，由“中国人权协会”负责人俞权域主编。《中流》由《光明日报》主办，林默涵、魏巍主编，有人依“中流”谐音和他们文章历来的风格，称之为“下流”。但《中流》常常引用陈一咨、方励之、严家其、王若望、苏晓康、魏京生等海外民主斗士的言论，有时甚至登载这些人的文章全文。虽然他们目的是进行批判，但却使国人因此了解到这些人的言论，这是国内其他媒体不敢做到的。<br />
