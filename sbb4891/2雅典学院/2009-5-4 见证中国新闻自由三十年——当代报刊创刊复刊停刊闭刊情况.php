年度，新闻出版署又批准成立以下报业集团：湖南日报报业集团、长沙晚报报业集团、吉林日报报业集团、长春日报报业集团、河北日报报业集团、杭州日报报业集团。到目前为止，全国的报业集团已有26家。其中四川的报业市场整合由于《蜀报》和《商务早报》的被砍受到了媒体评论界最大程度的关注。许多报纸因为不同意被吞并而停刊。这是一种打着集团化的幌子行“限制新闻自由”之实的举动。<br />
