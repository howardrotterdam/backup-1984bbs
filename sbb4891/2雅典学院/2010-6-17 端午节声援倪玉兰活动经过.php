&nbsp; &nbsp; <br />
&nbsp; &nbsp; 尽管6月15号发起“消夏端午节”前去倪玉兰律师居住的帐篷共进晚餐的苏雨桐被抄家并拘押，但昨日下午17时左右网友们还是从北京的角角落落赶到倪律师的住处--西黄城根公园的地下通道。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 大约晚上6点多，当网友莫之许、刘士辉、巴忠巍、李金城、张大军等十多位网友赶到倪律师处不到2分钟，4辆警车便开到了倪律师居住的地下通道，不由分说抬起她就走，众网友阻拦警察施暴未果，最终倪玉兰被台上警车拉走，众网友紧跟警车来到东华门锡拉胡同派出所进行围观声援。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 等笔者赶到锡拉胡同派出所，这里已经云集了莫之许、张大军、刘士辉、巴忠巍、张永攀、李金城、杨立才、华泽、刘沙沙、王译、天天、王阳等二十几位网友，网友们在派出所门口两侧席地而坐，有的网友就近买盒饭进餐准备打持久战，更有甚者打开手提电脑播放416福建马尾庭审三网友被诬陷案现场视频，画面不断切换416网友庭审围观现场网友们的英姿，还有网友高呼：言论无罪！自由万岁的呼声。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 期间网友不断赶来，刘沙沙、王译两位女士进派出所向警察问询倪玉兰的去向并向他们要人；值班警察则让她们出示身份证证明是亲属身份才肯受理问询，刘沙沙反让警察出示警官证，警察大怒：“我在派出所上班还有必要让我出示证件吗？”王译说：“公民有监督公务员警察执行公务的权利，我们要求你出示证件不过分，请问你们抓倪玉兰律师理由，警察以公务的名义肆意抓人也是侵犯公民的权利；这里是为人民服务的地方，不应该是侵犯纳税人权利的地方。”警察很不耐烦，把刘沙沙王译推出了派出所门外。张大军、刘士辉语重心长教育门口拿摄像机拍摄网友们的小国宝，小国宝只是麻木的说：“我的工作就是拍摄你们取证。”<br />
&nbsp; &nbsp; <br />
