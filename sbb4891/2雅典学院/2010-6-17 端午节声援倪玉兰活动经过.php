&nbsp; &nbsp; <br />
&nbsp; &nbsp; 雨越下越大，网友们的衣服几乎都被淋的透湿，11点钟，派出所左侧突然发生一阵吵闹声，原来警车警察已经包围了推友观光团，警察开始在左侧抓人，网友们一下子集中过去给警察要人，救出被抓的网友后，网友们果断撤退，他们收拾起帐篷，不约而同的高呼：倪玉兰！回家！倪玉兰！回家！倪玉兰！回家！然后两人一排列队，高声唱起雄壮的《国际歌》在雨中从容撤退。在静静雨夜中，这歌声显得尤其的嘹亮与震撼，警车与拍摄网友的便衣一直追网友至一公里左右，网友们停下自由组合坐的士全部撤离。<br />
