													<h2>李戡：我是李敖儿子 做什么韩寒第二</h2>
						
						
																											<div id="postmessage_787862" class="t_msgfont">南都周刊特约记者_陈婉容 台北报道<br />
<img src="http://www.nbweekly.com/UserFiles/fckfiles/2010/08/13/606d2a5c894a4a879cb49e7950c470fb.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
<strong>李戡</strong><br />
 <br />
　　李敖与现任太太王小屯之子，1992年8月3日生。今年以优异成绩被北大经济学院录取，因放弃台湾大学而择北大，成为公众人物。7月底出版处女作《李戡戡乱记》，痛批台湾历史教科书。<br />
 <br />
　　李戡，这名17岁少年，因高调报考北京大学而成了新闻人物，这源于他的一个特别身份――李敖的儿子。显然，在父亲的光环下他才得以被世人关注。<br />
 <br />
　　最近李戡出了一本批评历史课本错误的书，矛头指向民进党执政时期，“教育部长”杜正胜任意歪曲、曲解中学历史教科书，以政治介入教育，对历史课本进行洗脑性的修改，并且以“异族的角度写历史”，让所有中学生混淆是非。李戡多次出入台湾“国立编译馆”，对自己刚刚扔掉的历史教科书，进行多次证伪，书取名为《李戡戡乱记》。<br />
 <br />
　　台湾“大姐大”陈文茜评价说，李戡的“‘戡乱记’，一刀砍断台湾教科书想在他的脑袋里塞进的垃圾，再一刀砍断整个岛屿想包围他的窒息窝囊，‘戡乱记’成了他‘与成长之地的诀别书’”，“我的17岁比李戡滑头，也因此没有他的成就。他那么愤怒，因而产生了力量，宣告自己高中三年没白念，要以扎实的功夫找证据，并向世人证明‘杜正胜’们太可恶。”<br />
 <br />
　　虽然他还未满18周岁，但已继承了父亲的狂狷之气。他说，以前在中学时代，他在同侪之间就被当成一个大异类，他觉得很多想法和同龄人无法沟通交流。当然他也看不起那些“被教科书洗脑而价值观错乱”的同学。高中毕业后，他决定放弃台湾的高校，拥抱大陆的北京大学。不过，对于拥有万千粉丝的大陆青年韩寒，李戡也瞧不上眼，“韩寒算老几啊？他连大学都考不上。”<br />
 <br />
　　李敖的好朋友郭冠英，这位曾经以“范兰钦”为笔名在台湾发表统一言论而被开除职位的人，总是陪在李戡身边。而每次当李戡被记者问到关于对台湾的不满情绪时，李戡也总是以郭冠英的实际经历作例子，借此说明台湾的虚假民主让他多么嗤之以鼻。<br />
 <br />
　　李敖在陪儿子上节目推销《李戡戡乱记》一书的时候说：现在，我是李戡的父亲――李敖。言外之意，父以子贵，而不是子以父贵。<br />
 <br />
　　李敖曾描述自己的一生：“一个正确的人，活在错误的地方。”并且无比感慨写下伤逝的句子：“如今那复沧海日，钟声无恙我将归。”今年已经75岁的他，望着和自己整整相差57岁的儿子即将踏上他的未归之地，他满心骄傲和感慨，并在挚友陈文茜的节目里高声向北大女生们宣告：“李戡来了！”<br />
 <br />
　　李戡是李敖与现任太太王小屯所生。李王年龄相差30岁，李戡出生时，李敖已年过半百，不过，即使李敖说过这个孩子因为和他整整相差57年，光代沟就有好几条，但熟悉李敖的朋友都说，李敖总是把孩子当成孙子一样地疼爱。未满18岁的李戡，我们可以轻易地从他身上看见父亲李敖带给他的影响。他的批判精神、思想见解、用句遣词，处处可见李敖的影子。对于台湾的缺点，李戡毫不包容，并且和父亲一样，用“臭狗屎”来形容台湾的教育和台独氛围。直言不讳地宣称：自己再也不愿意和臭狗屎为伍了。文词犀利呛辣，颇有乃父之风。<br />
 <br />
　　李戡也坦言，自己阅读的课外书，全是父亲的作品，间或阅读一些父亲推荐的世界文学名著。所以，李戡的思想、世界观、批判性，基本上是由父亲李敖启蒙和建立起来的。<br />
 <br />
　　五年前李敖的神州之行，受到高规格的待遇，相继在中国第一流的学府北大、清华、复旦演讲，并且场场爆满，李戡一路上看见父亲演讲完之后众多热情粉丝和读者围住父亲坐的车子并且热切拍打着车窗玻璃的景象。父亲在群众中这样高高在上受人欢迎、尊重的画面，在当年年仅12岁的李戡心里，无疑是深受震动的，那时就让李戡在内心深处里觉得父亲实在很了不起。神州之行，使得父亲在他心里的位置更加高大、坚不可摧。<br />
 <br />
　　李戡在即将迈入18岁之际，作出了他人生的第一项重大决定――放弃台大，选择北大，是不是与父亲经历留给他的印象有关呢？<br />
 <br />
　　有人认为李戡小小年纪已学会了“政治投机”，也有人说李戡其实“很傻很天真”，对大陆包括大陆的教育体制并不了解。更有人相信在未来的时间里，李戡在经历了北大的洗礼之后，或会再出另外一本抒发对大陆教学不满的“戡乱记”。<br />
 <br />
　　<strong>“觉得我爸挺了不起”</strong><br />
 <br />
　　南都周刊：外界都对你父亲犀利和狂放不羁的风格印象深刻。你觉得父亲给你的成长经历所带来的影响是什么？<br />
 <br />
　　李戡：我觉得最重要的还是思考模式，还有我爸会教给我看问题实事求是的精神。他教导我教科书应付一下就可以了，他叫我多读文学作品，例如读他的著作或是一些世界文学名著，不要把眼光放在台湾这边。比方说俄国小说《卡拉马佐夫兄弟》。我在历史方面也受他影响，他思考批判的模式，我多少都受到一些影响。<br />
 <br />
　　南都周刊：我看过你和父亲一起上过陈文茜的节目，你父亲在你面前给人的感觉非常慈祥，和平时观众印象里的李敖不一样，我们在电视前看见他很柔软的那一部分。看了他在你书里写的序，他说从小他采取的就是放任的方式？<br />
 <br />
　　李戡：我父亲很少管我和妹妹。基本上一周只会在家两天，剩下的时间他都在阳明山的书房里面做自己的事情，写书什么的。他也几乎不过问我功课的事情，只有在联考要升大学的时候他才会说：“你至少要考上台大吧！”<br />
 <br />
　　南都周刊：从小到大，你父亲是你的偶像吗？<br />
 <br />
　　李戡：是有受到他很多的影响啦！但我也没什么偶像啊。2005年那时候陪我爸来大陆，看他在北大和复旦大学演讲的时候，那时候觉得我爸挺了不起的。<br />
 <br />
　　南都周刊：选择去北大念书，有父亲的希望在里面吗？<br />
 <br />
　　李戡：这是我自己的选择吧，我本来就比较喜欢大陆。而且经济专业本来就是我想念的科系，我的兴趣。<br />
 <br />
　　南都周刊：对于你到大陆念书这件事，你父亲的朋友郭冠英有什么看法吗？<br />
 <br />
　　李戡：他也非常支持我啊，因为他也看不起台湾搞台独的这些人。<br />
 <br />
　　<strong>“我相信北大是最强最好的”</strong><br />
 <br />
　　南都周刊：到大陆念书是你从上高中开始就有的想法吗？<br />
 <br />
　　李戡：当时就有作为一个选项考虑。我也梦想过去美国最著名的学校就读，但是后来又觉得我根本不需要到美国那种地方，到中国大陆就好了。我爸本来就讨厌美国人，我也讨厌美国人。如果我去美国的话，我父亲也会反对我拿美国绿卡。其实我也看不起台湾很多人，当然现在大陆也有这样的人，就是拼了老命去美国生小孩。所以我想我唯一能选的就是大陆吧！北大对于我来说是最合适的地方。<br />
 <br />
　　南都周刊：你是否考虑过，如果台湾的政府不敢开放承认大陆学历，你觉得这对你未来的发展会有影响吗？还是你就计划以后留在大陆，回归祖国了？<br />
 <br />
　　李戡：台湾政府承不承认大陆学历对我来说不构成什么影响啊！有些到大陆念书的台湾学生担心到大陆念完书回来之后，文凭变成一张白纸。可是问题是我不一样啊，我根本不担心这个问题，因为我又不回来台湾，台湾政府承不承认大陆学历根本不关我的事！世界各个地方都承认中国大陆学历，也就台湾不承认。<br />
 <br />
　　南都周刊：你在给北大的申请书上说，你家族很多人都念过北大，你父亲以没念北大为憾。但是，你或者你父亲是否有想过，后来的北大，和你亲戚当年念的北大，其实有很大的不同？<br />
 <br />
　　李戡：我没有考虑过这些，北大在以前是第一名，现在也是第一名，即使是整个大环境变了，我相信北大依然是最强最好的。而且以学生的水平来说，我相信北大学生也是超过台大的。所以我相信整体来说还是好的。<br />
 <br />
　　南都周刊：如果你发现，北大的校风并不如你预期的那样，当你感觉失望了，你会如何调试？<br />
 <br />
　　李戡：我想，我学的经济学专业也不需要什么学术自由吧？经济就是个很单纯的学科。台大这边也很黑暗啊，很多教授都是“台独”的。那些主张“台独”的教授可以占据一个系，然后打压学生。所以我不会去考虑学术自由的问题。拿郭冠英的例子来说好了，他在台湾谈统一，马上就被开除了。我不稀罕台湾的学术自由，我觉得那都是假的。大陆那边，我想大陆应该比台湾还不一样，而且我是学经济的，我不是学政治的，所以我也不需要什么学术自由，我不在乎啊！像我念的高中也很自由啊！可是我根本不需要这种自由，这种自由就只是所谓的染头发、烫头发，穿耳洞、穿鼻环。<br />
 <br />
　　南都周刊：我这里说的自由不是指这些，而是，比如言论自由。<br />
 <br />
　　李戡：我知道，但我不相信北大的言论自由会差到什么地步。而且，我也不是去北大发表什么言论的，我是去那边上课、学习的，所以我觉得这些对我来说无所谓吧！看情况再说吧！我也不晓得，到时候再看怎么拿捏吧！<br />
 <br />
　　南都周刊：你这本书《李戡戡乱记》，主要是痛批和纠正台湾历史教科书的错误。大陆有个中学历史老师袁腾飞，也在批评大陆历史教科书，你有没有听过这个人？<br />
 <br />
　　李戡：我没有听过这个人。但我想大陆的历史教科书即使有错误也只是近代史之后有错误吧！不会像台湾的历史教科书，根本就像是日本人写的。我根本读不下去。<br />
 <br />
　　<strong>“他们肯定不了解台湾”</strong><br />
 <br />
　　南都周刊：你了解大陆大学的学制吗？<br />
 <br />
　　李戡：我对大陆大学的了解，主要来自学生手册，就是学校发的学生手册。<br />
 <br />
　　南都周刊：很多人觉得你不是很了解大陆。<br />
 <br />
　　李戡：我想这么说的人，他们肯定也不了解台湾。他们觉得我把大陆想象得太美好了，但是同时他们也一样把台湾想象得太好了。很多大陆人以为台湾民主多自由，那是他们搞错搞混了，他们是先觉得大陆不好，才会觉得台湾多好多好。他们如果知道台湾的民主是什么样子，他们应该就会珍惜大陆这个地方。台湾就是个比较变态的地方。<br />
 <br />
　　南都周刊：你对大陆的理解是不是主要源于父亲影响？<br />
 <br />
　　李戡：我父亲多少有影响我一些。但是我父亲毕竟不是多愁善感的人，他没有和我说过什么关于他的乡愁。我本来就对大陆很有兴趣啊！大陆的风景、历史古迹、音乐、城市，我都很感兴趣。我也会看每周的电视节目《文茜世界周报》介绍大陆，我觉得还是对我有很多正面的影响吧！<br />
 <br />
　　南都周刊：除了2005年陪你父亲来大陆之外，你在考上北大之前，还有去过大陆吗？<br />
 <br />
　　李戡：有啊！我和我妈妈一起去过。我们去过苏州、上海和北京。我也希望利用一些机会，我能到内陆地方。比如我很想去壶口瀑布或者是到河西走廊看看，这些都是在台湾没有机会接触到的。希望我以后能利用课余时间多去大陆的更多城市看看。<br />
 <br />
　　南都周刊：平时会在网络上看一些大陆公共知识分子的博客吗？<br />
 <br />
　　李戡：我没有在网络上看过他们的博客，我上网都只是查查资料，收收邮件，更新一下微博。没有再看什么博客。大概就做做自己的事情，和朋友聊聊天。我不会排斥看别人的博客，只是我不晓得从哪里开始看，我觉得很麻烦，而且伤眼。<br />
 <br />
　　南都周刊：陈文茜稍前批评了大陆最有名的年轻人韩寒，你怎么看？也有人说过，你是第二个韩寒。<br />
 <br />
　　李戡：韩寒算老几啊？他连大学都考不上，连大学都没有念过，这种没念过什么书的人，我估计他也没读过什么经史子集，是只会玩赛车的人。最近很多媒体问我对他怎么看，我都回答烦了。<br />
 <br />
　　南都周刊：你现在已经有大陆朋友了吗？<br />
 <br />
　　李戡：呃……和我同年龄的好像还没有。主要就是我爸的朋友吧！网络上的应该也算吧！在新浪微博上有几个朋友我感觉蛮热心的。<br />
