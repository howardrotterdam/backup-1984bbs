													<h2>为什么中国不能乱 为什么西方民主制度不一定适用中国</h2>
						
						
																											<div id="postmessage_662969" class="t_msgfont">为什么中国不能乱，为什么西方民主制度不一定适用中国<br />
<br />
许多对中国现状不满的人士，认为中国的政治改革，需要向西方学习，通过政党轮替，开放报禁的方式，来求得人民的民主。<br />
<br />
更多的人认为现在的政府是在错误的道路上进行体制改革，特别是钳制言论，阻碍人民结社集会，造成了许多人的困扰和无奈，和愤怒的情绪。<br />
<br />
哲人曾经说过，多研究方法，少谈谈主义。<br />
<br />
我认为，政党轮替是民主的方式而不是目的。是的，这是方法，不是目的。不是说有了政党轮替，就是民主了，也不是说没有政党轮替，就不是民主了。<br />
<br />
民主的本质，是人民能够按照自己的想法，来安排公有的资源。和所有的国家一样，人民没有办法直接来处置公有资源，因此只好通过其他的方式，比方说非营利机构，或者，在中国，就是政府了。<br />
<br />
从这个角度出发，有许多值得探讨的地方。<br />
<br />
1. 人民能不能自己合理的安排？现在看来，没有什么地方，一定要说需要有个牧羊人才行。虽然从历史上来看，的确存在人民尚且不知道如何安排的情况。而且，这种的情绪，许多人存在，比方说出现大灾大难的时候，不少人呼吁无条件的服从政府的指挥。一听说政府，基本上是个褒义词。一旦听说中国派的舰队出国了，往往是自豪的心情。这一点，哪怕再是批评政府，认为政府和国家不一样的人，也不会怀疑。很少有人站在国际主义的立场上，来批评自己的政府。所谓科学是无国界的，但是科学家是有国界的，说的就是这个道理。<br />
<br />
大量的人对政府不满，不是对这个政体有意见，而是对这个政体的组织人员有意见（甚至有的时候是仇恨）。<br />
<br />
2. 人民的代理，能不能按照人民的选择来安排，而不是中饱私囊。<br />
<br />
今天中国的主要矛盾，其实就是腐败一个。所谓不患贫，患不均，指的就是这个意思。<br />
<br />
认识到这点很重要，这就是为什么改革不能脱离中国的国情。中国的国情就是大量的中国人，还是对这个国家/政府自豪，虽然对其中的坏分子痛恨。<br />
<br />
其实，政府本身也是一而再，再而三的和这样的现象斗争。即使其中的官员自己阳奉阴违，背地里是个腐败分子，在公众场合，还是会冠冕堂皇的鼓励人民和腐败斗争。<br />
<br />
换句话说，再是糟糕的官员，也不敢说自己就是为了贪污。不像是之前的糊涂分子，认为贪污腐败是市场经济的推动力什么的。<br />
<br />
这意味着，凡是反对贪污的人们，就拥有了话语权。<br />
<br />
那么如何才能解决贪污问题呢？是不是通过西方那样的政党轮替，开放报禁就可以做到呢？<br />
<br />
西方所以能够成功的进行政党轮替，而没有发生骚乱，是有其几百年的文化沉淀的。相反，我们看看泰国，就会发现，同样的民主安排，并没有良好的开展，而是政府用武力解决。这样留下的仇恨的种子，这样在国人心灵上的创伤，需要很多年才能平复。<br />
<br />
我认为这个还是需要从中国的实际来看。<br />
<br />
中国人有个特点，叫做抢打出头鸟，但是中国也有个特点，叫做法不责众。如果你把这两句话放在一起，你就知道我们的选择是多么的狭小。<br />
<br />
因为许多中国人不喜欢出头露面，所以本来需要大家一起努力的问题，被搁置起来。这个和西方不同，许多人有独立人格精神，如果认为不对，就会站出来，在问题还没有越来越大的情况下，通过报纸，诉讼等等手段，把社会矛盾消化掉。比方说同性恋等等。<br />
<br />
但是中国呢，由于高压政策，使得人民往往不太愿意站出来，除非是到了所谓忍无可忍的地步。结果问题越来越大，等到真的有人不顾一切出来的时候，已经是非常不可收拾了，那个时候，由于太多的人身同感受，哪怕这个触发点不是和自己有关，但是也会变成了墙倒众人推。突然之间，局部的问题演化成了全局性的问题。<br />
<br />
但是政府为什么会在一开始采用高压制度呢，也是因为看到了如果墙倒众人推的时候，那些推动的人，会对社会造成非常大的冲击。这个时候，政府的选择，就是把所谓的不稳定因素扼杀在萌芽状态。<br />
<br />
结果越是高压，越是遏制了小不稳定，而积累成大的不稳定。越是为了拖延大的不稳定和可能的破坏作用，而越是高压，结果就造成几十年可能没有什么变动，但是最后崩盘。中国两千年的历史，就是循环这样的过程。<br />
<br />
所以中国的问题，不是政党轮替，不是开放报禁，而是如何能够通过一个个小的不稳定，来疏导社会的不满，从而避免大的不稳定的发生。<br />
<br />
我认为，在今天这样矛盾已经比较多的情况下，不加限制的政党轮替，是比较让人忧心忡忡的。但是允许地方报纸批评地方的实际施政方针，从而凝聚社会共识，却是有益的。</div>

							
							
							
							
