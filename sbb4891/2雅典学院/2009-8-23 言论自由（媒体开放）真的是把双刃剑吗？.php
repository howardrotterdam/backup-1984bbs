																											<div id="postmessage_215027" class="t_msgfont">言论控制的理由辨析<br />
转自 <a href="http://www.huangdianlin.net/?p=162" target="_blank">http://www.huangdianlin.net/?p=162</a><br />
<br />
<br />
1、两类限制言论的理由<br />
<br />
对言论自由进行限制和控制的各种理由，似乎很多，但归纳起来基本上是两条：<br />
<br />
第一，教条主义的理由：某些言论是错误的。<br />
<br />
从这个角度看，对某些言论进行限制的理由是认为这些言论是错误的。这样说的假设性前提是认为，关于任何事物的见解或者学说只有一种观点才是正确的，因此与此不同的观点都是谬误。为了保护这个唯一的真理，必须消除错误言论的扩散。<br />
<br />
这样的逻辑无论是中国还是西方，都曾经非常盛行。西欧中世纪的宗教教条主义，以及中国文化大革命时期的狂热的个人崇拜，都是典型。两者都把自己的教条神圣化，唯一化，认为除了自己的观点之外的一切论点都是邪说，取消了一切不同意见存在的合法性。<br />
<br />
在一切极权或威权制度中，由于对事物的定义和解释的权力是被垄断的，因此，这种教条主义的逻辑始终是存在的，而且是这种制度存在和延续的基础性要素之一，通过这种方式极权制度确立了自己对唯一正确性的控制。<br />
<br />
第二，实用主义的理由：某些言论是有害的。<br />
<br />
这种观点认为，不管某些言论在理论上是否正确，但从实际效果来上说，他们可能会对社会秩序的稳定构成威胁，对社会的正常发展和有序改良是有害的，因此，应当加以禁止。<br />
<br />
在一个威权社会中，由于政治权力的稳定性依靠的是一个强力集团的政治压力以及通过改良所获得社会发展的绩效所获得暂时的合法性，因此，这种制度体系对本身的稳定性有一种极端强烈的要求。一切突破这个底线或者对这个底线构成潜在挑战的言论，都被视为是有危害的，不管从逻辑上或理论上它是否是正确的。从现存制度的角度说，此类言论，越是真实危害就会越大，因此，发表这种言论的人就应当受到更严厉的惩罚。此类例子在当前的社会现实中很常见。例如，以颠覆性或者煽动性这样的理由来控制某些言论。或者以泻露机密为由启动压制措施。<br />
<br />
2、对两类理由的辨析<br />
<br />
（1）某些言论是错误的，因此，需要限制。<br />
<br />
这里所谓“错误”意味着对某一事物真假值的判断。一般说，判断是一种认知行为，是对观念与观念是否相符合的一种确认或者否认。因此，为了辨析这种言论限制的理由，需要从哲学上分析判断的诸种形式及其可能性。<br />
<br />
判断是对两个事物或者观念之间关系的表述，因此需要至少两个要素，分别称为主词和谓词。根据一个判断中主词和谓词的关系，可以分为分析判断（从主词可以推导出谓词，与经验无关）和综合判断（无法通过分析主词得出谓词，需要通过经验归纳得出）。<br />
<br />
康德把分析和综合判断与“先验”和“后天”（经验）范畴结合起来，提出了四种判断的分类：先验分析判断、经验分析判断、先验综合判断、以及经验综合判断。<br />
<br />
在这四类判断中，由于分析判断是一个通过前提推导结论的逻辑过程，因此永远是先验的，在逻辑上可被证明对还是错，故第二类经验分析判断是不存在的。而第四类判断，经验综合判断，本质上就是依靠经验观察得出的判断，因此这种判断在一定的经验范围内可以被判定为对还是错。而至于第三类，先验综合，则是诸如诸如数学定理或道德律令这样的判断类型。在康德哲学中，这是他要着力说明的一种判断类型，但他的这个努力没有完全成功。其中道德性的判断，本质上是一种价值判断，无所谓绝对的对错。例如，对什么是低俗或者什么是色情的道德判断，很大程度上是一个历史时代的文化和社会实践约定俗成的规范，一般会随着历史的进程发生变化。因此，对这样的言论没有充分的理由通过强力的方式进行禁止，相反，应该通过充分的言论自由集中最多数的意见形成共识性意见，根据这个一定历史条件下的共识对道德言论进行规范。例如，现代社会的基本共识是色情信息不应当提供和涉及到非未成年人，只要不违背这个基本的原则，色情信息的生产和消费应当通过法律渠道进行规范，而不是通过压制言论的方式。<br />
<br />
因此，对言论控制的问题来说，最关键的是两类判断：先天分析判断，即逻辑命题，以及经验综合判断，即经验命题。对逻辑命题一类的言论来说，无需对其进行言论控制，因为在逻辑的范围内，任何不符合逻辑规则的命题都无法成立，能够存在的命题在逻辑范围内都是正确的。将此类言论认定为错误，只能以不讲逻辑的方式实现。<br />
<br />
对经验命题一类的言论来说，判定这种命题是否则正确，尽管表面上在一定的时空范围内可以实现，但是依然存在这样的问题：经验命题是通过试图穷尽经验现象来得出的判断，但其实这种穷尽是不可能绝对实现的，因此，经验命题不可能被绝对证实。典型的例子是：地球围绕太阳转。这个命题曾经依据一定条件下的经验观察被认为是错误的。因此，对经验命题的判断，只能在一定范围内的经验事实的基础上进行，并且不能保证这种判断的绝对正确性。<br />
<br />
因此，无论是逻辑判断还是经验判断，都无法绝对支持以错误言论为借口对言论进行限制。对逻辑命题的言论可以通过逻辑自身得到解决，而经验命题即便在定的历史条件下看起来是错误的，也不能就此被认定为绝对错误的。因为，在经验领域，那个作为评判错误言论的绝对正确真理的位置并不存在，任何确信不移的“真理”都只有在特定的历史条件和认知框架内才是有效的，因此都不是绝对正确的。对自然规律的判断是如此，对人类社会的判断则更是如此。所以，任何依靠绝对真理的权威来压制“错误”言论的做法都是不合法的。<br />
<br />
从某种意义上说，即便是在一定的逻辑和经验条件下被证明为错误的言论，也不是完全没有价值的。因为即便是谬误，也可以成为促进人类认识进步的催化剂。如果没有改革初期那些当初被视为“错误的”思想的解放力量，很难想象会有改革开放的今天。 如果只有绝对正确的唯一的真理，想象力将彻底死亡，人类的知识和社会的进步、文化的繁荣将会彻底停滞，因为一切谬误已经被禁止存在，人类不再存在进取和探索的动力。<br />
<br />
（2）某些言论是有害的，因此，需要控制。<br />
<br />
这里的问题关键在于，某种言论“有害”也是一个判断。只是这一判断是根据某种预期的经验进行的判断。根据上述第一条的分析，从判断命题的逻辑上可以得出类似结论认为：这样的判断是很难成立的，况且这种判断大多是基于对尚未发生的“事实”的预期。例如，社会的动荡。<br />
<br />
此外，这种论调还存在如下问题：<br />
<br />
第一，这种判断试图建立一种因果关系：某种言论导致某种有害的结果。姑且不论在实际经验中，是否真的存在这样的有害的事实。假设真的发生这样的事实，要回答的问题在于，如何证明某些言论就是这种事实的原因？从逻辑上说，因果关系是最难以证明的关系类型。与相关关系（即不同事物之间的关联性）不同，对因果关系的证明在逻辑上和经验上都存在极大的困难。<br />
<br />
第二，如何定义“有害”，对谁“有害”，以及由谁来定义什么是“有害”的。如果对这个“有害”的定义只是政府的判断或者一小部分掌握话语权的利益集团的判断，那么这样的判断本身就是成问题的。因此，任何对言论危害性的判断，都不应当是一个武断式的压迫性判断，相反，应该是按照一种社会共识的界定，例如按照一定社会阶段的道德准则对色情言论加以规范和限制，以及基于普遍利益的基础提出的界定，例如煽动暴力和仇恨的言论是对人身安全的一种危害，因此必须禁止，等等。但即便是这样的限制，也应当是在一个公开、公正的法律制度框架内实施。总之不应当是由任何一个个人或者组织或者集团来加以界定，无论它是领袖人物，还是一个政党，还是一个政府。<br />
<br />
第三，言论“有害”论，其实反映了一种深刻的对非理性后果的恐惧心理，即假定某些言论会带来秩序混乱的非理性后果。但悖论正在于，对非理性的恐惧正是源自充分言论自由的缺乏。充分言论自由的基本功能在于为社会成员提供一个相对公开、透明、真诚的表达环境，可以将社会的信息不均衡和由此带来的混乱程度降到最低，可以将一个社会及其成员从由于信息压制所导致的非理性中解放出来。<br />
<br />
因此，以“有害”为理由来限制言论只有在极其紧迫和明确的情况下才能够适用：<br />
<br />
a. 要为压制言论提供理由，必须存在畏惧的合理依据：一旦实行自由言论，严重危害就将产生；所忧虑的危险必须迫在眉睫，所将产生的危害必须是严重的，否则无论在道德上是否应受谴责，都不应受到限制。<br />
<br />
b.只要有时间通过自由讨论揭示谬误，合适的方式是更多的言论而不是强制沉默;只有在言论将立即产生危害，因而不可能通过讨论加以预防，政府才有理由限制或惩罚言论。<br />
<br />
c.要控制言论，危险必须是真实而非猜测的，是清楚而非模糊的，是即刻就要发生而不是遥遥无期的；否则，公开与充分讨论完全可以防止危险发生。<br />
<br />
综上所述，两类控制言论的理由都是很难成立的。人们对这些控制言论的理由之所以相信，是因为没有在逻辑上辨析和认清这些理由内在的矛盾之处。<br />
<br />
言论自由不仅是现代民主社会的基石，而且从根本上说也是一切谋求进步、繁荣、稳定的社会所必需的基本制度安排。一个没有言论自由的社会，意味着充满着言论禁忌或言论恐怖，其结果只能是压制真实的声音，谎言盛行，诚信沦丧，道德崩溃。在一个能够自由安全表达真实想法，以诚信为荣的社会里，没有人愿意撒谎。人们由于无法自由表达自己的真实想法，而生活在愤怒和对真实情况缺乏明确了解的恐惧之中，社会的暴力倾向和非理性倾向会大大增加，社会不稳定的程度会日益严重。从这个角度说，对言论的压制就是对和谐秩序的破坏，因为它不仅违背人性，也违背逻辑规则，同时更违背社会发展的规律。<br />
<br />
(这篇笔记参考了张千帆老师的讲义。若有政治学和哲学专业人士看到，请多指教，谢谢。)</div>

							
							
							
							
