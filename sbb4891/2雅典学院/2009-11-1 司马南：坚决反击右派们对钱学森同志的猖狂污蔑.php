													<h2>司马南：坚决反击右派们对钱学森同志的猖狂污蔑</h2>
						
						
																											<div id="postmessage_308893" class="t_msgfont">钱学森晚年倾注大量心血的一个领域<br />
司马南＼文<br />
<br />
&nbsp; &nbsp;钱学森先生在工程控制论和系统科学等领域辛勤耕耘，为新中国科学事业国防事业所做出的卓越贡献，全体中华儿女乃至国外科学界同仁，都是有目共睹的，连他的敌人也不得不承认。但是，另有一个领域，钱学森先生晚年倾注了大量的心血，在这个领域中，钱学森先生迄今尚未得到科学共同体多数的理解……<br />
<br />
科学上对与错的判定，是一个复杂而严格的问题，需要实践的检验，也需要时间的检验。作为一个始终洋溢着爱国主义激情的伟大的科学家，钱学森先生用毕生的经历探索自然和宇宙的奥秘，并致力于将自己的学识奉献给祖国和人民，其晚年倾注大量心血研究人体特异功能问题，就其动机而言，相当程度上与此毕生追求有关，仅此一点，便不能不令人肃然起敬。<br />
<br />
 ３０年前的那场关于人体特异功能真伪的大争论，将许多人卷入了“承认与否”对立的两大阵营。人体特异功能肯定派的领军人物，即是我国航天之父科学泰斗钱学森先生，否定派的领军人物则是社会科学领域百科全书式的大学者于光远先生。２０年前，两位大家在全国政协礼堂互不相让针锋相对的讲话，各为其追随拥趸者所盛赞所诟病，至今争论声犹在人们耳边回响，而作为辩论一方的钱学森先生今天业已谢世，永远地离开了他所眷恋和捍卫的事业，辩论的另一方９５岁高龄的于光远先生也已风烛残年久卧在床。<br />
<br />
争论并没有结束，真理的探讨也并没有停止。<br />
<br />
在缅怀纪念钱学森先生的这个特殊的时候，在有些人看来，也许这方面的话题显得不那么合乎时宜。所以，在怀念钱学森先生所有的文字中，鲜见有人提及特异功能的内容，颇有一点为尊者讳的味道。<br />
<br />
余以为，这种避讳，大可不必。<br />
<br />
作为介入此事较深的一个后来者，笔者愿意介绍一点情况，借以表达对钱学森先生的崇敬和怀念。<br />
<br />
 1980年2月至４月间，上海和重庆两个很偏门的会议引起了钱学森先生的关注。<br />
<br />
一个是２月4日至10日，《自然杂志》编辑部发起组织的“人体特异功能科学讨论会”在上海科学会堂召开，贺崇寅代表《自然杂志》编辑部在会上作了《为探索人体生命科学的奥秘而进军》的发言。应邀参加的人体特异功能儿童有四川大足县的唐雨，北京市的姜燕、王强、王斌，安徽宣城县的张雪梅，湖北武汉市谢朝晖、熊洁、徐倩，江苏常熟市的郑红，河北沧县的于瑞华，黑龙江桦南县的牟凤芹。另一个会议是3月2日重庆市科协召开人体特异功能座谈会，在这个会上，时任四川省委书记的杨超亲临讲话表示支持。<br />
<br />
6月4日，钱学森先生访问《自然杂志》公开亮相，他从开发人的潜能的角度表示支持人体特异功能的研究。他审慎地说：“一项新的科学研究，在刚提出的时候，总是有人反对，带头的人也总是要受到反对，因此要有勇气。要挺住腰板。”就是在这一次谈话中，他首次提出了“人体科学”这个概念。<br />
<br />
1981年2月20日，《人体特异功能通讯》第7期刊发《钱学森教授谈人体特异功能》的文章，文章报道了钱学森先生在听取了北京师范学院物理系林书煌刘惠谊等人组织的人体特异功能研究小组工作汇报后所作的发言。钱学森在这一次特别强调说：“人体特异功能的研究工作很重要”，“现在承认的人越来越多了，将来一定会更快地发展起来……”<br />
<br />
１９８１年3月9日，中国科协主要负责同志专门举行会议，邀请《自然杂志》贺崇寅同志汇报人体特异功能研究工作的进展情况，到会的有裴丽生、刘述周、王顺桐、聂春荣、林勃民等，会上一致同意支持4月在四川举行第二届人体特异功能科学讨论会。钱学森先生亲自参加了会议。<br />
<br />
１９８１年4月20日，《人体特异功能通讯》第9期报道了中国科学院四川分院院长刘允中《谈人体特异功能》。他披露说，“这个事情发展很快，在那样一番波折下，能有一些人坚持搞下去，发展到今天这个状态，得到钱学森等着名科学家的大力支持，很不简单，相当不错。历史上多少人为研究一门科学献出生命。向真理迈近一步是相当不容易的”。<br />
<br />
1982年5月5日，钱学森给时任中宣部副部长的郁文写信，向他陈述并以党性担保人体特异功能是真的，不是假的。信中对中宣部依照时任总书记胡耀邦的指示精神所下发的通知提出批评，钱学森在信中说：“中国科协四月廿八日通知说，‘耳朵认字’之类不是我们科研方向，不准在报刊上介绍和宣传。您是知道的，一到下面去执行，就会一棍子打死。上海出版的《自然杂志》就被命令，将即发排的五月号中撤出几篇有关人体特异功能的科学研究论文。……难道前车之鉴还少吗？不是发动批判过摩尔根遗传学吗？还有批判控制论，批量子化学共振论，批人工智能；还有批数量经济学，批形象思维……我建议您通知上海市宣传部门的同志，正确处理《自然杂志》的问题，不要禁止它刊登科学论文。”<br />
<br />
在1982年10月召开的人体科学筹委会第三次会议上，钱学森莅临会议作了题为《这孕育着新的科学革命吗？》热情洋溢的报告，他提出了一个激动人心的猜想，对气功和人体特异功能现象的意义作了更高的概括和推论：“我想真正吸引着我们沿这条曲折而又艰险道路去探索的是：这可能导致一场21世纪的新的科学革命，也许是比20世纪初的量子力学、相对论更大的科学革命。我们当中谁来作这场未来科学革命的启蒙者？谁呢？”<br />
<br />
1983年3月14日，钱学森在航天医学工程研究所（又称５０７所，亦即今天的“中国航天员训练中心”）作“关于科学道德”的报告。<br />
<br />
有人统计，自此至1987年10月5日，钱学森先生在该所共作了100多次报告或发言，这些报告和发言涉及人体科学、系统科学、气功、中医、特异功能等问题，这些讲话后来整理成《人体科学与现代科学纵横谈》一书（笔者手中至今存有此书）。<br />
<br />
１９８２年3月30日，《文汇报》发表中国科学院上海脑科学研究所所长张香桐《对脑研究的哲学思考》一文，此文点名批评了特异功能现象，钱学森由此发出了“我看这位科学家有点跟不上时代”的感叹和批评。<br />
<br />
1986年2月23日，钱学森在中国气功科学研究会召开的座谈会上发言指出：当前气功科学研究的一项任务是建立唯象气功学。此后“唯象气功学”“唯象人体科学”的提法不胫而走。他说：“什么叫唯象科学？就是只知其然，不知其所以然。”他兴奋地做了一个展望：“我国有十亿人口，如果每一百个当中就有一个练功，就是一千万，每百个练功的人就有一个人去教，就需要十万个气功师，把这十万个气功师提高提高，就是一件大事。……还有一个尖锐问题，就是实践表明，气功可以练出特异功能来。……到那时，我们这些炎黄子孙也就无愧于自己的祖先，应闻名于世了。”<br />
<br />
１９８６年，清华大学几个人研究风靡一时的严新气功，轻率地给出了若干令科学界_目结舌的结论，钱学森先生1987年4月18日对两位主要研究者陆祖荫、李升平等说，气功是我们国家的国宝，它有很高级的功能表现，有很深奥的内容，对此非但不能怀疑，而且要大力支持。<br />
<br />
１９８６年5月3日，国家科委批准成立中国人体科学学会，钱学森亲自担任名誉理事长，国防科工委原副主任张震寰将军为理事长。<br />
<br />
钱学森先生在他所倡导的人体科学方面著述甚多，表达坚定，他把中医气功特异功能联系起来对问题进行分析，不仅在科学方法和科研方向上作出指导，而且对社会上的各种议论和倾向也有针锋相对的回应：“我认为，中医、气功和特异功能是三个东西，而本质又是一个东西。中医是经过宪法肯定了的，尚且还有许多人不承认。当然，现在正在逐渐改善。更何况气功和特异功能？”<br />
<br />
钱学森先生将社会上人们对特异功能等现象的不解和批评，归之于西方医学科学还没有发展到我们这个阶段，他主张把人体看做一个“巨系统＂，“用系统辨识的方法”去解决问题。“气功所涉及的问题完全是一个新的领域，气功的研究会使我们找到一把打开人体科学大门的钥匙。我们要把人和生物看成是一个复杂的系统，于是在外界给它一个信号，它就可以自己去完成一套行为。把这个信号叫做信息或密码都行。它们的能量可以很小很小。关于生命信息，今天我们有了这样的想法，是有条件了。因为有了气功和中医研究的发展。我们不能怪西方医学，因为它没有发展到这个地步。我们的中医没有西医许多规律、原则的束缚，所以有许多问题倒猜对了。但是，对中医也不能句句都信。因为中医是产生在它的那个时代的系统辨识结果，而我们今天必须前进。我们要把人看成一个巨系统，用系统辨识的方法去解决这个问题。<br />
<br />
科学上的对与错的判定，是一个复杂而严格的问题，需要实践的检验，也需要时间的检验。作为一个始终洋溢着爱国主义激情的伟大的科学家，钱学森先生用毕生的经历探索自然和宇宙的奥秘，并致力于将自己的学识奉献给祖国和人民，其晚年倾注大量心血研究人体特异功能问题，就其动机而言，相当程度上与此毕生追求有关，仅此一点，便不能不令人肃然起敬。<br />
<br />
不可否认的是，钱老关于气功和特异功能的一些具体的论述，确曾被一些神功大师所利用，他们打着钱老的旗号，大量制造钱老支持他们的谎言。必须指出，骗子的行径与犯罪行为概与钱学森先生本人无涉，钱学森先生的清白善良无私与忠诚永远是我们学习的楷模。<br />
<br />
钱老晚年所倡导的人体科学到底有多少真理性的内容？在多大程度上会被科学家共同体所承认？相信科学发展本身的规律，将最终作出公正的回答，历史绝不会淹没钱学森先生晚年科学探索的成就，亦同样不会虚伪地敷衍一些不实的钱老所厌恶的溢美之词。实事求是与怀疑精神，同为科学家精神世界与完整人格的两个方面，这便是人们常挂在嘴边上的科学精神四个字的内涵。<br />
<br />
钱学森先生学界楷模国人榜样灵魂不死永垂不朽！<br />
