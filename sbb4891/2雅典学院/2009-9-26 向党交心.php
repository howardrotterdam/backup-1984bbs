																											<div id="postmessage_259349" class="t_msgfont">浦志强：向党交心<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;――我在June Fourth清查运动中提交的反思<br />
&nbsp; &nbsp; 1989年，我在中国政法大学法律古籍整理研究所读研究生，全程参与了春夏之交的学生运动，发起和组织了学校几乎所有的罢课、游行、绝食活动，并于6月4日凌晨最后一批撤出广场。早在1984年大学期间，刘泽华先生为我们历史系的本科生开设中国历史研究法课程，他曾经布置我们思考文革时期发生的人与人之间的揭发和反思洗脑情形，是否还会在新时期重演。我们认为改革开放和思想解放运动已经如此深入人心，这种事情应该不会再有了。刘先生不以为然。<br />
&nbsp; &nbsp; 毕业后，反对资产阶级自由化的运动发生了，各地的表态和学习反思都发生了。那时我正在读一套写文革的书――《历史在这里沉思》，对刘泽华教授的布置的问题颇有新了些的认识。<br />
&nbsp; &nbsp; 1988年，考入中国政法大学读中国古代法律文献研究方向的研究生。当时正是官倒横行、腐败抬头、改革走入死胡同的时候，大学里的思想比较活跃，这是一个人心思变风起云涌的关头。随后学潮发生并被镇压。<br />
&nbsp; &nbsp; June Fourth以后，我回到家乡，开始想开学后会怎么样，会不会上演刘先生所说的清查、反思、提高认识的把戏，想看一看大家怎么坐在一起说言不由衷的话，提高自己本来不糊涂的认识。开学后的学校的第一件事，果然是布置大家接受清查，学校还专门成立了清查办，没什么新招数。<br />
&nbsp; &nbsp; 清查的事情自然是严肃认真的，但学校对自己的学生还是充满善意的，我在书中读到的反右时期和文革期间的剑拔弩张没有出现，理由可能因为谁都清楚发生了什么，想想昨天还在一起游行抗争，今天怎么好意思死盯着不放呢？所以清查应该说是走了过场，无非是大家都表态赞成中央的英明决策而已。<br />
&nbsp; &nbsp; 我的性子执拗，不认为自己有什么罪过，同时想到一个有几百万人参与全国人卷入的运动，要是绝大多数人都反思了和提高认识了，似乎全都是上了Zhao Ziyang的当，恐怕不妥，总要有人承担责任。我不过是一个学生而已，以我的地位和影响力固然不足以承担什么责任，但在我自己没有想通的时候就大口炎炎地号称共产党对了自己错了，至少对不起自己的良心，也容易给共产党错误的信号。于是迟迟不能拿出上边满意的结论。<br />
&nbsp; &nbsp; 1990年底，我们毕业在即，对我们的处分措施也到了不能再拖下去的时候了。学校组织部、清查办和研究生院学生处要求我就与我有关的几件事再写一份东西说说清楚。这几件事都是在一年多的清查过程中，公安局手持“证据”或外单位前来学校向我调查他人的行为时反映出来的。思前想后，决定把事情的前因后果和可以说的经过讲出来。写的过程中情绪难平，似乎闭上眼睛就能回到当时，一切历历在目，写出来的当然就更加不符合学校的要求。我觉得我没有什么可说的，也没发现有什么问题，更不需要低头认错，该认错不是我们，至少主要不是我们。写好后，加上一个题目，就叫做“向党交心”。我当时想的是，1957年知识分子向党交过一次心了，但热脸贴了人家的冷屁股，这回我的交心，是交给我自己的，告诉自己跟人家尿不到一个壶里。我想既然跟共产党的缘分早就走到头了，还是分开好一点，以后的事情那就以后再说吧。这颗心一旦交上去，我的处分可能马上就下来了，随便怎么样吧。<br />
&nbsp; &nbsp; 1990年12月30日，我被告知处分结果：行政严重警告处分，好像还有团内的严重警告处分。巧的是，这一天恰好是我入团的十周年纪念日，我终于对我自己有了一个交待。1991年1月，我领到了研究生毕业证书和硕士学位证书。<br />
今天想，可能共产党要的其实不是人们真心的反思和服从，压根就是让人人表面上低头，让人人都瞧不起自己面对现实。我没有这么做，我现在心里头一直很踏实。虽然我十几年来失去了很多机会，但也并不后悔。我想，以后再有要人说谎往自己头上泼脏水的倒行逆施，主动合作的人会少了很多。<br />
<br />
<br />
　　　　　　　　　　　　　　　　　　　　浦志强　2004年4月28日<br />
