<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;向&nbsp;&nbsp;党&nbsp;&nbsp;交&nbsp;&nbsp;心<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;古籍所八八级研究生&nbsp;&nbsp;浦志强<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;一九九O年十月廿七、廿八<br />
<br />
&nbsp; &nbsp; 一九八九年的北京之春事件，以人民解放军奉命平息反革命暴乱为标志，走完了它五十多天的历程。作为这个共和国成立以来的第一次大的危机，它将给国人留下深深的思索。作为这一历史事件的当事人，当我在一年之后的今天，试图从难以平抑的情绪中摆脱出来，伏案沉思时，还是难以客观全面地考察自己当时的所作所为。我承认我是一个弱者，虽然我时刻希望能面对自己的良心，像卢梭那样无情地解剖自己，但始终觉得难以做到。现在想就几件事谈谈我自己的看法。<br />
&nbsp; &nbsp; 首先是五月十九日在长城饭店接受美国ABC广播电视公司采访一事。我记得因绝食的缘故，我在五月十八日患上了急性肠炎，并于当晚住进了北京妇产医院，而且是乘中国红十字会的救护车去的。次日凌晨八时许，回到天安门广场上我校帐篷里。九时左右，一位穿灰色茄克衫的男性公民走进来，问我是否可以作为中国绝食学生的代表接受一下记者的采访，我答应了。然后与他同来的两个人扶着我，将我带到纪念碑二层南面，我坐在那里等了半个多小时，这个人又回来请我跟他走。他们扶我走到广场外中国银行旁的便道上，那里停放一辆日本丰田牌大面包车，我上去后发现里面已经有五六个人，似乎他们中彼此成分各不相同，有外地进京声援的学生，有医生、有青工，而我则作为绝食学生代表。他们告诉了我们将去长城饭店进行采访后，车就开了。由于体力原因我睡着了，直到车停在饭店门口。有人扶我走进电梯，上到某层，进了某个小房间。先是坐在一起聊天，我躺在软床上养神又喝茶，并按照绝食者可以允许的进食标准，喝了两杯牛奶，吃了几块巧克力。这时主人告诉我们12点正式开始采访。采访之前先叫了一桌饭菜，我因尚在绝食而未进餐。采访是由这个公司的主持人在美国问话，由坐在我们面前的翻译译成中文，然后我们用中文回答，他再译回去。我记得问我的问题大意是：中国总理LiPeng说，绝食再也不能持续下去了，你认为绝食是否还会出现第八天、第九天？我回答的大意是：我们绝食时立下的誓言是要取消“四二六”社论对于动乱的定性，并且现场直播官方与学生代表的对话。现在我们已经进行了七天的绝食，政府方面并未答应我们的条件，也没有作出任何让步，所以如果政府方面如此坚持下去的话，绝食不仅会出现第八天、第九天，而且要一直坚持下去，直到政府答应条件为止。对其他人的发问我已经记不清了。好像问了一个学生：政府会不会用军警镇压？大概这位学生说：“人民政府怎么会把枪口对准人民呢？简直不可思议”云云。就我当时来想，我认为政府太不象话，我们都快要饿死了，你们还这样毫无人道，于是我的态度才强硬起来。我相信我的这种心态与大家相同，我们都是为了帮助共产党和政府把自己的毛病改掉，谁会想要推翻他呢？当然LiPeng当时有他自己的考虑，具体情况我们无从得知，我们也不知道由于我们的行为，全国已经出现了动乱的迹象。不过就我现在来想，我仍然不认为有一个什么组织在领导这次学潮，并有预谋地使它升级为动乱和反革命暴乱！否则就无法解释为什么这次学潮表现出这样强烈的无序性，并且在坦克出现在北京之后，学生、市民均作鸟兽散这一事实。我是历史系毕业的大学生，又讲授过中国革命史课程，在党史上我没见到共产党搞学运有哪一次像八九年北京学潮一样的情形。因此，我只能对“四二六”社论在当时的定性持保留意见。<br />
&nbsp; &nbsp; 第二件事是五月二十七日左右在天安门广场上“自由论坛”上我发表演说一事。我记得当时声援的人已不太多，工人每天上班，晚上又涌进广场来听广播。那天先是一个学生在上面声嘶力竭地嚷一些话，接着又是一个自称为什么纠察队还是敢死队的队长在讲他们的绝死之心。好像他说如果坦克开过来的话，就让坦克从他和他的手下人的身上碾过去，并说虽然他相信解放军会这样干，他也并不害怕云云。我好像是第三个上去讲的，与他们相比我自惭形秽，于是我在自报姓名之后（主持人要求每个人都要先报出自己的单位、姓名），说道：“与前面两位相比，我是灰溜溜地爬上这个椅子的，我既没有第一位那样高昂的气势，又没有第二位那样显赫的官衔，我不受任何组织的任命，也不负担任何具体的职务，我只不过是在这个小小的广场上绝食了短短的八天！”。鉴于当时陈云等人已在报上发表了谈话，说牺牲了两千多万人的生命才换来的江山不能让极少数极少数人夺走云云，联想到八八年我考研究生时的一个判断题（题目是判断“贫雇农打江山坐江山”的正误），我批评了陈云等人的讲话。我当时认为二千多万人牺牲了生命，建立了这个共和国，并不意味着这两千多万人以外的他们的同志们永远坐江山，否则这些人不成了独裁者吗？其他的话已经记不太清楚了。现在回过头来想，我觉得我可能有些过分，也许是我误会了陈云等人在那个特殊时期讲话的含义，但以一介书生之愚见，我还认为“贫雇农打江山坐江山”的说法代表了一种产生于小农经济思想基础之上的皇权主义思想，当然这是学问上的事，这里不再提下去了。<br />
　　第三件事是六月三日中午领一千元人民币的事情。六月三日中午，几个学生告诉我，在新华门口静坐的学生都被从中南海里冲出来的军人用警棍、树杈子、木棍子打了出来，他们请求支援，并且给我看了他们身上的伤创。我觉得军队也太不文明了，学生的体质哪里禁得住他们那身好拳脚呢！当然我没意识到由于学生坐在那个地方会使共和国丢这么大的面子，也就是说没有意识到问题的严重性以及军人将学生打跑的重要性和必要性。我去了纪念碑，碰见了李录，他是学生领袖，大名鼎鼎。我说我是政法大学的，我们的哥们儿被当兵的在新华门打了，能不能支援一下，别让军人把市民再打了，惹出更大的事儿来。他说行，又问，你们那儿需不需要钱？我说要是有那当然好啦！他说那你写个报告，就说新华门保卫处需钱一千元罢，我签字你就能领到送去。我就写上了“新华门保卫处需活动经费一千元”字样，他签了字后，领到钱，送到新华门，见到一直和我校学生一直在一起的黄伟（维？），见他脖子上挂个扩音器，正在指挥学生纠察队，将狂怒的市民与激动的士兵隔开。我就给了他手里拿的一千元，说这是李录拨下来这儿用的。他接过去后，我们发现士兵又用警棍打倒了三四个市民，有人从后面开始往里边仍石头、酒瓶等东西。于是我也成了纠察队员。直到当天下午四、五点钟光景，才回到天安门广场。由于情况特殊，而且我也不是学财会的，因此我没想到，要是当初让这个黄伟开个收条给我的话，我就不会在今天有这么大的麻烦。现在坏了，我不知道他是哪个单位的，只知他好像在广州搞外贸，是个商人。现在我只能以我的人格和良心作证，我没有花费过不该我花的一分钱，除了买了一条运动短裤之外（大概在五月十八号中午，我因肠炎不慎将大便排在短裤内。我买短裤时有人陪同前往），如果我的人格和良心还能得到一定程度上的承认的话。<br />
&nbsp; &nbsp; 第四件事是六月三日晚在“民主大学”成立大会上的演说问题。在我的记忆中，好像在当晚十时左右，流血冲突的消息就不断传到天安门广场来，我只能说我为事态这种悲剧性的进展感到痛心。我不愿学生、市民流血，正如同我不愿士兵流血一样，当时因为我认为这种结局一出现，将会不可避免地埋下怨恨的种子。当时广场上尚有数万人不肯散去，其中大多数集中在广场北部民主女神像的附近，等待着民主大学的开学典礼。我转到那里时，大概在晚间十一时左右，人山人海，万头攒动，中间有一排桌子，再往前是一张放在木板台上的桌子，上面放着一个话筒，由一个学生在宣布民主大学的成立以及校长、名誉校长和教授的名单，好像这些角色都是由国内一些有成就的知识分子担任的。然后由一位和尚讲了几句我听不太懂的话，我当时胸中升腾起一股要讲话的念头，就跑上去讲了几句心里话。我总感觉我生长在这样一个国度，习惯于专制的重压，看惯了国人的愚昧与麻木，甚至从我所读的鲁迅先生的文字中，得出了我们生活在“暂时作稳了奴隶”的时代的结论。我感到阿Q的阴魂并未因黄龙旗的落地而消失，我们还熟睡在如先生所说的封闭的铁屋子之内。我在想，我们这一代知识分子应当具有献身精神，通过良知与呐喊将熟睡的人唤醒。于是我将眼光投向了启蒙思想家的丰碑。大概民主政治对我这样的青年人的诱惑太大了，以至于我们将它当作了荡涤独裁专制的灵丹妙药，如此则通过民主大学培养出反封建的勇士，岂非中国人民应当额手相庆的伟业？基于这种想法，当时我为民主大学的开办而欢呼，于是我登上了那个讲台，对着我的听众，开始了我的即席演说。我讲到：我要以一个民主大学学员的身份，庆祝民主大学的诞生。自五四以来七十年间，民主与科学的口号激励着中国人中间的优秀青年为它抛头颅洒热血，而今天我们才以我们的智慧与牺牲将我后面这位美丽的仙女（指民主女神）由天上引到了人间。我还说新中国成立了四十年，中国人民并未能像毛泽东所宣称的那样站起来，共产党几十年的统治给我们带来的只是奴隶和奴才的地位，我们要让他们知道，中国绝不会在“沉默中灭亡”（鲁迅语）。最后我用中国人对皇帝的虔诚喊了声：民主大学万岁！以上所述不是原话，但大意恐怕没有太大的乖谬。现在想来，我为我的幼稚而哑然失声，中国的事情如果像当时我所想象的那样简单，新中国不早就超英赶美，跑步进入共产主义了吗？我现在想，共产党统治中国四十一年，犯下了这样那样的错误，而如果换了其他政党，也未必不会犯那样这样的错误。中国历史的进程，不是一直十分符合其内在的合理性而体现出马克思主义者所常说的那种必然性吗？共产党有失误，也有成就。抛开这些不谈，它不也是几万万中国人自己的选择吗？把一切都归结到共产党甚至归结到它的领袖们身上，是不公平的事。试问面对中国的历史，生活于其中的非领袖们，就不应该扪心自问，引咎自责吗？明末清初的思想家黄梨洲曾讲过：“天下者，天下人之天下也！”，鲁迅先生笔下的夏瑜说： “这大清的天下是我们大家的！”，孙总理中山先生说：“管理这个国家的权利，为全体国民所共有，非数人所得而私也”。我也要说，这中国，是中国人民的中国，每个人也都应为它的成就而感到自豪，每个人也都应为它的劣根而“每日参省乎己”，怨天尤人，贪天之功为己有者，动辄诿过于人，不是具有完善的人格的人应有的态度。<br />
&nbsp; &nbsp; 以上，就是我就几件事情所作的追忆与反思。风平浪静一年多了，我承认我无时不在反思过去，思考未来。作为此生至今我所遇到的第二件大事，它像十几年前的唐山大地震一样，会随我走完一生的路程。或许，上面还有许多值得商的地方，或许，若干年之后的我会改变现在的看法，我想这都不重要，我坚信我会像梁启超先生一样“不惜以今日之我与昨日之我战”。我相信：明天会更好！<br />
&nbsp; &nbsp; 这是一个青年人对这个国家，这个民族的良心！</div>

							
							
							
							
