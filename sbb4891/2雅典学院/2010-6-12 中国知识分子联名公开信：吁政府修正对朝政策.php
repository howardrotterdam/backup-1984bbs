													<h2>中国知识分子联名公开信：吁政府修正对朝政策</h2>
						
						
																											<div id="postmessage_682237" class="t_msgfont"><a href="http://blog.sina.com.cn/s/blog_5a4a94d10100k4nk.html" target="_blank">http://blog.sina.com.cn/s/blog_5a4a94d10100k4nk.html</a><br />
<br />
知识分子联名公开信：吁政府修正对朝政策(2010-06-12 20:47:36)<br />
<br />
&nbsp; &nbsp; 6月10日，一些关心中国外交政策的知识分子联名发表公开信，呼吁中国政府正视现实，废除《中朝友好合作互助条约》，结束中朝军事同盟关系。公开信指出，今日的朝鲜政权早已成为当代史上道德形象最丑恶的巅峰；其内外交困局势加剧，极有可能出现罗马尼亚式剧变。公开信呼吁政府从国家长远利益出发，改变战略缓冲思维，停止支持因濒临崩溃的金正日政权。<br />
<br />
&nbsp; &nbsp; 中朝关系在边境枪击事件后进入微妙时期，中国外交部发言人在沉默了三天之后表示，对朝鲜此举进行了“严正交涉”。6月10日，一些关心中国外交政策的知识分子，发出一封公开信，开放联署，呼吁中国政府正视现实，改变对朝绥靖政策。公开信说，多年来，中国百姓一直相信宣传中的中朝伟大友谊，但事实却远非如此。公开信呼吁从国家长远利益出发，改变战略缓冲思维，停止支持因濒临崩溃的金正日政权，为后金正日时代的朝鲜半岛早做准备。<br />
<br />
&nbsp; &nbsp; 公开信全文如下：<br />
&nbsp; &nbsp; 朝鲜是关乎中国安危的最重要邻邦，近期朝鲜内外紧张局势加剧，极有可能出现罗马尼亚式剧变，为国家安危计，我们呼吁中国政府明确改变对朝政策。<br />
<br />
&nbsp; &nbsp; 朝鲜自1990年代中期大饥荒以来，陷于完全崩溃的经济局面始终未得扭转，全赖中国及韩、日、美等国际社会输血救援，才未再度发生大规模人道主义灾难，而金正日政权亦得延续至今。<br />
&nbsp; &nbsp; 今天，朝鲜因退出六方会谈、悍然核试验、枪击韩国游客以及驱逐国际救援组织人员等行为，已基本断绝了除中国之外的外部输血。<br />
<br />
&nbsp; &nbsp; 去年底，金正日旨在彻底摧毁地下经济的货币改革遭受重大挫折，朝鲜再度出现人相食的悲剧；与此同时，金正日正式明确了将政权交给儿子金正云的接班安排。为转移社会强烈不满，以及为儿子接班铺路，金正日又发动了新一轮清洗。<br />
<br />
&nbsp; &nbsp; 朝鲜虽长期处于高压锁国状态，但今天朝鲜从普通百姓到统治阶层，对金氏王朝统治的极度愤怒，早已是人所共知的事实。近期韩国天安舰被击沉事件，正是朝鲜政权岌岌可危，不惜极端手段铤而走险的表现。而金正日请求中国援助的神秘之旅刚刚结束，就发生3名中国边民被枪杀事件，更是证明，今天的金氏朝鲜，无论中国付出多大的忍耐和牺牲，已无可能施以任何善意影响。<br />
<br />
&nbsp; &nbsp; 近十年来，朝鲜政权的行为愈发偏离理性，在其内外局势日益严峻的情形下，我们完全无法预料金正日接下来将会有何种疯狂举动。如无意外，遭货币改革、肃整地下经济的人祸打击和年初春寒天灾打击的朝鲜，不久将再度发生大面积饥荒。而金正日健康江河日下，政权控制力减弱和统治集团向心力的急剧衰竭，在目前人心涌动的朝鲜，随时有可能发生齐奥塞斯库政权垮台式的戏剧性转折。<br />
<br />
&nbsp; &nbsp; 60年前，为了朝鲜的生存，中国倾全国之力参加朝鲜战争，付出巨大的人员伤亡。但战争刚结束，朝鲜就开始排斥志愿军，并极力否定中国在战争中的巨大牺牲和贡献。战后，贫困的中国长期予朝鲜以巨大经济援助，但未赢得任何善意。尤其近年来，中国在给予朝鲜维系其生存的关键援助时，朝鲜官方却一直在公开批判中国的“改革开放”。<br />
<br />
&nbsp; &nbsp; 多年来，中国百姓一直相信宣传中的中朝伟大友谊，但事实却远非如此，1952年中国召回驻朝大使后，这一岗位空曾缺近3年。随后的朝鲜大清洗，与中国有历史渊源的官员被全部清洗。在中国倾尽全力的2000年奥运会申办活动中，朝鲜投下了“宝贵”的反对票。而近年来，朝鲜几乎每次都要在中朝两国领导人见面不久，立即搞出惊险动作，以给外界造成中国支持或默许的印象，无情地予中国以羞辱。<br />
<br />
&nbsp; &nbsp; 今天的朝鲜政权，早已成为当代史上道德形象最丑恶的巅峰。中国虽然坚持不干涉他国内政的原则，但对朝鲜这个最丑恶的政权，中国事实上的作用，却是以《中朝友好互助条约》为其提供军事保护，以经济援助避免其政治崩溃，同时却陷2300万朝鲜人民于极权恐怖和饥饿之中。<br />
<br />
&nbsp; &nbsp; 中国对朝鲜的援助、支持和妥协忍让，使中国承担了巨大的国际道德压力。尤其是在中国大批遣返朝鲜难民、抓捕闯入驻华外交领馆寻求避难的朝鲜难民时，中国的国家形象在全世界面前蒙上了空前耻辱。<br />
&nbsp; &nbsp; 一些人认为，中国应当支持金正日政权继续存在、以使中国获得一个战略缓冲区，但他们或许未曾认真想过，金氏朝鲜当年发动战争前，朝鲜半岛并无美国驻军，而且朝鲜战争停战后，朝鲜不但无数次主动挑衅，还在三八线下多次开掘输送兵员、装备的巨大隧道，正是金氏政权的存在，才有了美国强化东亚驻军和重新武装日本的结果。<br />
<br />
&nbsp; &nbsp; 这些人恐怕也未曾想过，朝鲜人民痛恨自己历史上总是成为大国角逐对抗的战场，以今天韩国国民的普遍心理，朝鲜南北统一后，驻韩美军不可能继续留在朝鲜半岛。有那个民族愿意自己的领土成为大国对抗的前线？<br />
<br />
&nbsp; &nbsp; 朝鲜南北统一是势之所趋，像当年东西德令人措手不及的统一情形，随时会在朝鲜半岛出现。而中国之于朝鲜的影响和地位，远无法与苏联当年之于东德可比。如果朝鲜南北统一前的最后一幕，依然是中国无条件支持朝鲜金正日政权，而非积极为朝鲜半岛的早日统一做善意准备，则60年来中国人民为朝鲜付出的无数鲜血和汗水，换来的很可能是6000多万朝鲜人的仇恨。<br />
<br />
&nbsp; &nbsp; 今天中国对金正日的支持，只会给统一后的朝鲜留下这样的印象：朝鲜民族长期分离巨大的悲剧，尤其是朝鲜北方2300万人民这些年来的悲惨命运，最大的因素就是中国。<br />
<br />
&nbsp; &nbsp; 中国人民对今日朝鲜式的悲剧曾有切肤之痛，我们从这种灾难的边缘走来，我们深知自由和发展的可贵，我们希望2300万朝鲜人民和我们一样，通过经济和政治改革走出梦魇。<br />
<br />
&nbsp; &nbsp; 中国领导人也曾为此做出努力，希望以中国经济改革的成功范例，劝导朝鲜领导人为改善民生进行改革。但言之谆谆，听之藐藐，朝鲜当局依旧我行我素，甚至枉顾中国安危在中朝边境进行核试验，准备随时把中国拖入战争泥淖。<br />
&nbsp; &nbsp; 朝鲜金氏世袭政权的本质，决定了朝鲜北方已丧失了通过渐进改革方式走出困境的可能，也决定了它绝无稳固长存的可能。<br />
<br />
&nbsp; &nbsp; 基于金氏政权一再挑衅和破坏东亚安全体系的事实，基于金氏政权给朝鲜人民带来的沉重灾难，基于朝鲜军方对中国的敌对而非友好态度，基于为后人建构和平友好中朝关系的新架构的期待，基于中国作为一个负责任大国的道义责任，我们提出以下对朝主张：<br />
<br />
&nbsp; &nbsp; 一．废除《中朝友好合作互助条约》，结束中朝军事同盟关系。朝鲜对亚洲安全秩序的挑衅，使中国承担了不对等的战争义务。俄罗斯在修订与朝鲜关系的过程中，已废除了类似的军事同盟内容。<br />
<br />
&nbsp; &nbsp; 二．把对朝援助项目转为联合国规划下的人道主义援助项目，并由联合国监督执行，确保援助用于人道而非军事目的，也不得使其用于其特权阶层的奢侈生活。<br />
<br />
&nbsp; &nbsp; 三．在朝鲜拒绝恢复六方会谈和朝鲜半岛无核化，拒绝回到《核不扩散条约》的情况下，不与朝鲜发生任何实质性军事关系，不提供任何可直接用于其军事目的的战略物资，严控其设在中国境内各种商社组织的资金流向。<br />
<br />
&nbsp; &nbsp; 四．为防止金氏政权孤注一掷的非理性行为，做好随时保持对其核设施进行外科手术式摘除以及对其在中朝边境军队解除武装的准备。<br />
<br />
&nbsp; &nbsp; 五．为金正日政权统治的崩溃做好积极准备。包括必要时，在联合国授权的情况下，在中朝边境朝鲜一侧建立国际难民营的准备，以防止该政权崩溃后大批难民涌入中国，冲击中国东北地区正常社会秩序。</div>

							
							
							
							
