													<h2>奇文共赏：佛教是吃人的宗教吗？</h2>
						
						
																											<div id="postmessage_754365" class="t_msgfont">在国内最具“邪气”的佛教网地藏论坛上，<br />
有一个帖子赢得了一片叫好声，现将奇文共赏如下：<br />
==========================================================================================================================================<br />
地藏菩萨保佑我，顺利送走一个债主菩萨！！！<br />
作者：李十方&nbsp; &nbsp;&nbsp; &nbsp;发表于：2004-7-31 19:42 <br />
<br />
有师兄问到我流产的事，我想就我个人经验讲一讲，让大家对佛法更有信心。<br />
&nbsp; &nbsp; 三年多前，我意外怀孕，那是我第一次怀孕，虽然不是很想要这个孩子，但想一想，来了就要生下来，于是就开始准备。<br />
&nbsp; &nbsp; 我怎么说这孩子是大债主呢？打从我一怀上就一直有股很强的冲动要吃鸡，其他什么都不想吃，就是要吃鸡。可是我那时已吃素八，九年，看到肉都不会有流口水的反应了，何况是自己想要吃？根据台湾人的说法，怀孕时口味改变，都是因为小孩想吃的东西跟妈不同所致。所以我说是这个孩子想吃，强迫著我去找鸡来给他吃。<br />
&nbsp; &nbsp; 那是一种很奇怪的感觉，我明明就不想吃鸡，但我心里一直有股意念冲上来想吃鸡。真的很难形容。<br />
&nbsp; &nbsp; 我怎么会为他去吃鸡呢？于是我很明确的，而且三番两次的趟叨亲永锏恼夂⒆樱你在我家是找不到鸡吃的，而且到你长大搬出去住，自己到外面去找肉吃之外，你是不可能在家里找到肉的。<br />
&nbsp; &nbsp; 我还建议他是不是投错胎，要不要考虑换个家之类的话(我当时心情很不好)。刚开始这孩子很不听话，折磨得我要死，我几乎天天哭。我吃什么都不行，什么都是很苦的味道，心里就是要吃鸡。吃不好就睡不好，半夜醒来都是想著要吃鸡。<br />
&nbsp; &nbsp; 那真是一段我生命中很黯淡的日子！<br />
&nbsp; &nbsp; 后来有一段很短的时间，稍稍吃得进一些东西，我以为这孩子妥协了，学著吃青菜豆腐了，正高兴呢，又不行了，想吃鸡的意念如排山倒海似的向我扑了过来。<br />
&nbsp; &nbsp; 我那时天天念地藏经(每天两品是晚课的一部份)。边念边给这孩子讲道理，我觉得很有效，念了之后食物比较能吃得下去。但是我怀孕过程中身心的折磨让我相信来的肯定是个债主，所以我有一天开始念梁皇宝忏(没有拜忏，是用念的)，坐在佛堂慢慢念了一个下午，约三个小时，只念了头四章吧，并讲经中的道理给孩子听。<br />
&nbsp; &nbsp; 每天也帮孩子向佛法僧三宝叩头，每天一有空就跟这孩子讲佛经中的道理让他明白，当然我也向他忏悔过去对他做的不对的事，我也多次明讲希望他自己走。念完梁皇宝忏后，果然孩子马上变得很乖，没有再闹，我开始生活复正常，吃得下也睡得好，而且不想吃鸡了。我想梁皇宝忏真棒。<br />
&nbsp; &nbsp; 约十天后我去产检，医生说要让我听孩子的心跳，结果测了半天也找不到心跳，医生问我有没有流产或任何改变，我说没有呀，没有东西流出来，我也没去撞到肚子或任何异常呀。我还不知道发生什么事呢。为求慎重起见，我去照另一种超音波，这次证实孩子已无心跳，简单讲，这孩子已胎死腹中了。<br />
&nbsp; &nbsp; 而且医生根据孩子脑壳的状况趟呶遥这孩子大约是产检前十到十二天走的。算算时间，刚好是我念梁皇宝忏当天或之后的日子。我饮食习惯的恢复正常也确实了医生的计算。那时大概是怀孕四个月吧。<br />
&nbsp; &nbsp; 医生趟呶遥一般常见的流产是孩子自己流掉，我这种胎死腹中型比较少，如果孩子自己掉不出来，就要刮宫，但刮宫是有很大的危险性的，基本上医生拿著金属夹子到子宫里把孩子夹出来，讲得简单，但做起来就知道是很危险的，医生要凭感觉来摸你的子宫(没有两个人的子宫大小是一样的)，在子宫里找到孩子，然后完全夹出来，一不小心，子宫可能被刺穿孔，刺出好几个洞来造成大出血，或没夹干净会有后遗症等，反正讲得让我理解，这个手术很危险。<br />
&nbsp; &nbsp; 是呀，我突然想起报上说某个妈妈堕胎时大出血，有的甚至造成下肢瘫痪或死亡的。<br />
&nbsp; &nbsp; 结果等了两个星期，孩子都没掉出来(肚子里带著一个死的孩子生活两周，各位能想那是什么日子吗)，我只好动手术。<br />
&nbsp; &nbsp; 手术之前和当天我都求菩萨让我手术平安，结果手术是医生口中的出奇顺利。我看得出医生真的很高兴。医生开了两大瓶共一百颗最强力的止痛药给我，但手术后我一点也不痛，也没流什么血，大概只用了两片卫生棉吧血就停了，手术完马上可下床走。对不起，如果这让各位有些恶心，但我只是想让各位了解我做完了一个流产手术后不痛也没什么流血，是不是有些奇迹呢？<br />
&nbsp; &nbsp; 你可以说怀孕四个月本来就是流产高峰期，但我流产的时间和念梁皇宝忏的时间有巧合，这怎么讲？<br />
&nbsp; &nbsp; 后来我猜测这孩子用胎死腹中的方法来走的原因，我想，他是趟呶遥走是他的决定而不是我的决定。如果这孩子是自己流出来，那我一定这辈子都会猜想是不是我去撞到什么东西，可能是我的疏忽导致流产而一辈子愧疚在心。如果是胎死腹中的方式，那是说他的神识走了但他的身体留给我处理，这样的话我没有什么愧疚，不会觉得我没有照顾好他导致流产。<br />
&nbsp; &nbsp; 我看到另一人的故事是孩子都快出生了，最后孩子(是个债主)要走且决定用脐带绕脖而死，好巧，那也是属于胎死腹中型，要手术才能拿出孩子！<br />
&nbsp; &nbsp; 现在我与我同修已断了正淫，地藏菩萨(占察轮)给我们两个不邪淫─大善的成绩，我不会再怀孕了，但想起这段往事还真心有余悸，冤亲债主来讨债时真没有一天好日子过，所幸佛菩萨和梁皇宝忏帮助我们过这段难关，不过我们在事情发生时并不知道事情会这样圆满结束，也不敢妄想佛菩萨能如此满我们的愿(我们一直很希望孩子能自己走)。<br />
&nbsp; &nbsp; 我们当时唯一凭仗的就是靠对佛菩萨的信心和忏悔来撑过来的。菩萨再度满了我们的愿！<br />
&nbsp; &nbsp; 这是我的经验，希望能加强各位师兄学佛的信心。<br />
