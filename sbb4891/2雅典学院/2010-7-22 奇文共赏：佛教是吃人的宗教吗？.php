作者：dandanwang2006&nbsp; &nbsp; 发表于 2009-10-29 16:16 <br />
<br />
我现在高兴的心情，无法用言语表达，我想把今天遇到的事情说给大家听，因为我太激动了...<br />
<br />
前两天，我妈妈去寺庙，法师送给我一些书本和DVD，我回家来看。这两天看的是净空法师的讲座，他讲得真的很好！<br />
<br />
还有一个DVD，叫山西小院，讲很多患病的人，诵读：地藏经，结果病就好了。<br />
<br />
我拿出了很多佛经，有我自己去寺院请回来的，也有法师送我的，唯独没有一本地藏经，我有点失落。<br />
<br />
这两天，我腰疼又犯了，三四天都在家里，要么卧床，没有办法外出，我的心著急地想早点读地藏经，以便早点回向给我的冤亲债主。<br />
<br />
今天一大早我上网，就搜索地藏经，结果一看有20页，我家打印机墨水不够，只打了3页。在我卧室，有地藏王菩萨像，我拜地藏王菩萨说：今天我要诚意诵地藏王菩萨本愿经，可惜只有3页，我恳请菩萨加持，早日赐我一本完整的经书，以便我认真读诵，回向冤亲债主。<br />
<br />
拜完后，我就开始认真读，虽然不是很长，但是由于我肺部积水，胸闷气急，读起来也相当困难，读完后，读回向时，不知道怎么的，心酸就哭，哭完人又清醒了，不知道怎么回事情，为什么这么难过。<br />
<br />
我在客厅又念地藏菩萨名号，有十多分钟。<br />
<br />
正好我妈妈外出买菜回来，一进门就说：有缘啊！她手里多了2本书，递给我时，其中一本是地藏经，正是我向菩萨求的，另一本是大悲咒。<br />
<br />
我妈妈说，今早去买菜，不知怎么，竟和一个买菜的人说，女儿信佛。那人便欢天喜地说有缘，就带我妈回她家，取了2本书送我。<br />
<br />
我妈回来，我和她说我拜菩萨求书的事情，她也很诧异，而且是同一本书，就一直说灵验灵验，现在我妈妈也相信佛了。<br />
<br />
有求必应，一点也没错！<br />
<br />
我太感激了，我一定要花时间好好读经书，以报地藏菩萨赐书之恩，在我最需要的时候，她显应了！！！<br />
