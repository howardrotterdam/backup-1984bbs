[内容提要]<br />
所有宇宙之谜中，最难解、最恼人，而对个人和社会又至为切近急迫的，是关于揭谜者自身的谜：人从哪里来？生命渊源于何外？人只是一架思维机器，还是具有所谓“灵魂”的半神灵？人在宇宙中的地位如何？人一死永灭，还是有来生生世？这些问题中，从生死之谜，尤其是死后有无续存的问题，关系到每个人最切身、最根本的利益，对它的解答，是人们决定人生态度、人生目标，建立人生观、价值观、伦理观的基石。揭开生死之谜，无疑成为人类智慧面临的要务。从某种意义上讲，对生死之谜的破解，要比衣食住行条件的改善、工业新产品的发明等现实问题，更为现实，更为重要。<br />
自远古以来，被社会多数人乃至全体信奉认同，据以建立人生观、伦理观、价值观，作为全部生活支撑点的对自身生死之谜的解答，主要由各种宗教所提供。<br />
在所有宗教中，以教义体系最为丰富深厚著称的佛教，与多数主要依感情需要仰赖神灵救赎的宗教不同，是以智慧究明自身，以求解脱生死等痛苦为主旨，以“自净其心”、“如理作意”为超出生死的要道。佛教以“缘起”的朴素辩证法观察生命现象，认为众生的存在是非断非常、即生即死而又因果、身心相续不断的无究无尽的流转过程，人必有前生后世，死后必受生前行为的规定，轮回于天、人、鬼、畜、狱“五道”中，备受诸苦。佛教对生命现象、宇宙全景论述之广度、深度、明晰度，确非世间同类学说可与伦比。<br />
