<br />
永恒轮回<br />
　　讲一点佛教的原理，也是叔本华、尼采哲学的基本观点；与犹太一神教思想和唯物主义思想大不相同。<br />
<br />
　　佛教不承认灵魂的存在，却主张轮回，这个很多人可能觉得不可思议：没有灵魂，怎么轮回呢？其实很简单，下面是尼采的一段话，可以清楚地说明轮回的原理：<br />
&lt;&lt;&lt;&lt;&lt;&lt;<br />
　　“现在我死不复生，”你将说，“顷刻之间化为灰烬，灵魂也将像肉体一样死去。但我被纠缠其中的众多原因将再发生，――它将把我再造出来！我自身，就是永恒复归的众多原因的组成部分。我将与此日，此地，此鹰，此蛇同归――不是一个新的生命、更佳的生命或相似的生命――我将永恒地返回到这完全相等的、同一的生命，在最大与最小的事件中，再次教诲一切事的永恒复归。”<br />
&gt;&gt;&gt;&gt;&gt;&gt;<br />
　　当然，这里我要声明一下的是：尼采说的这个人不是一般人，而是拜火教教主查拉图斯特拉，是某种意义上的完人，所以才会以完全相等的方式复归；佛教所讲的轮回，一般没这么精确，只要相似就可以了。<br />
<br />
　　从藏传佛教的转世办法我们可以看到，认定转世的依据是相似性。探访转世灵童的人，会在孩子们的身上观察他们是否有前任活佛的特征，如果他们之间的很多习惯爱好都相吻合，就可以认定他可能是前任活佛的转世灵童。<br />
　　人们可能对这种转世办法有一种很自然的，却不准确的理解，觉得这仿佛是拿着照片去机场接人，对上号了，就是他了。其实不然。拿照片去机场接人，那个人是谁已经固定好了，就算你拿着照片对上号了，这个人也可能不是他而仅仅是一个和他长得一摸一样的人。<br />
　　但佛教却不承认灵魂，也就是说，不存在一个固定好的前任活佛的灵魂需要我们去接；而是说，产生前任活佛的那些因缘业力，又把他再造了出来。只要灵童和活佛足够相似，我们就可以认为活佛转世了。<br />
　　换言之，就是说：不是轮回造成的相似，而是因为相似所以被称为轮回。<br />
<br />
　　我讲到这里，有人可能说了：哦，我明白了！所谓轮回，指的不是“我”的轮回，而是说一个与“我”相似的人还会再现，这种轮回还真没意思啊！<br />
　　这个理解基本上是对的，但光说轮回就偏了。佛教的基本原理，用上面的视角看，也可以算“没意思”（当然实际上不是如此）。因为佛教最基本的一个观点，就是“无我”。今生今世的“我”都是“无”的，轮回又岂能是“有”的？<br />
　　佛教解释“无我”用意最多，我要是一一复述，几本书也写不完。索性用一个最简单的（就不管准确不准确了），也就是西方唯心主义哲学的基本原理之一：“世界是一个梦”这句话是不可证伪的。别的就不论了吧。<br />
　　在“无我”的基础上，“轮回”是实际存在的，不是因为“轮回”真实，而是因为“轮回”与“我”的真实性没有本质区别。这个话应该能自圆其说了，不过到底为什么呢？<br />
<br />
　　“我”实际上是不存在的，但是，除了已经自证涅*的人以外，我们一般人总是认为“我”是存在的。为什么呢？这不是因为“我”真的存在，而是因为我们的生存意志（叔本华提出的概念，基本上就等于“生物本能”），告诉我们“我”是存在的。认定“我”是一个实体，昨天的“我”也是“我”，明天的“我”也是“我”，这些理念，并不是理性的结论，而是我们作为一个生物固有的本能。人类的生物本能当然是把自己看成是活着的，从昨天一直活到明天。<br />
　　接下来是关键的部分：人类不仅仅是生物，而且是社会生物，社会性是构筑在人类本能之内的**，也就是说，社会性也是人类生存意志的一部分。因此，人类并不仅仅认定自身的真实存在，整体的延续性也是人类本能中意识到的东西之一。这就是轮回的基础。<br />
　　换言之，“我”本来是不存在的，但是人类的生存意志告诉“我”，自己是存在的；“我”本来也是不能轮回的，但是人类的生存意志告诉“我”，自己是要轮回的。什么时候“我”真正悟道了，涅了，也就最终脱离了存在与轮回。<br />
<br />
　　因为这个可能不大容易理解，再换种方式敷述一下：为什么我会认为，10年前的我和10年后的我，都是我呢？因为10年前的我、现在的我、10年后的我，都是很相似的，而且生存意志也告诉我，我就是这么生存下来的。为什么大喇嘛会认为他是转世而来的呢？因为他跟前任大喇嘛也是很相似的，历代大喇嘛承担其民族和宗教命运的意志，也是一如既往，薪火相传。<br />
<br />
<br />
*：每次讲课或讲点什么，都会被概念的互相牵扯而发愁，因为不可能一次把它们都讲清，比方涅这个概念，我就不想讲，但是它确实需要出来。涅，就是对生存意志的一种否定，涅以后的人，不仅看清了自身的本质是“无”，而且是从感性上认识到了自身的本质是“无”。这就仿佛戒烟，你知道吸烟有害健康，强忍住不吸，不算圆满，看到烟都没感觉了，就是圆满了。涅就是对我执（我所执着的事）的一种去除，而且是从感性上的去除。去除以后，因为不再担心利害，生活就如同玩游戏了，而且是那种完全放松心情的玩（而不是痴迷者那种自虐式的玩）。可想而知，涅以后，也就跳出轮回之外了，因为轮回终究是由生命意志而产生的。<br />
**：养过猫狗的人肯定清楚，你喂养的方式一样，猫狗对人的态度可是大不一样的。狗是社会性的动物，因此它很容易把你看成是狼群中的头狼；而猫就没有狼群这样的社会组织，所以和你的关系就是另一回事。这就是社会性本能的作用。</div>

							
							
							
							
