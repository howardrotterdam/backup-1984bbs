陈兵，1945年生于甘肃武山。1968年毕业于兰州大学中文系。1981年毕业于中国社科院研究生院宗教学系，获哲学硕士。曾任中国社科院世界宗教所助理研究员，现任四川大学宗教学所研究员、博导，兼河北禅学研究所副所长、成都市政协委员、四川省文史馆馆员。著有《佛教禅学与东方文明》《佛教气功百问》《道教气功百问》《道教之道》《佛教格言》《新编佛教词典》《生与死》《重读释迦牟尼》《二十世纪中国佛教》（合著），参编《中国道教史》《道藏提要》《道教手册》《诸子百家大词典》等。在《哲学研究》《世界宗教研究》《法音》《内明》等刊物发表论文百余篇。现从事佛教心理学的研究，带有佛教专业的博士、硕士生二十多人。<br />
本书在中国大陆地区首次用通俗语言全面系统地揭示有关佛教生死轮回的奥妙，是一九九七年度在日本、中国台湾地区最畅销的图书。<br />
