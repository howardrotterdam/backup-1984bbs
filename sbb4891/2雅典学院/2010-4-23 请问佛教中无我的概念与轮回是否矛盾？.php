绪言<br />
　　生死之谜――人类理智面临的尖刻挑战<br />
　<br />
　　以“智能生物”自命的人类，生来便陷入了智能与愚昧（用佛教术语，可称为“明与无明”）矛盾冲突的困扰中。诚如庄子所慨叹：“吾生也有涯，而知也无涯！”无限的未知领域，如同宇宙空间那浩渺深邃、隐藏着无穷奥秘的黑暗，紧紧包围着人，人每走一步，都必须燃起自心智能的火炬，去开辟道路。解谜，似乎是人的天赋使命。解不完的宇宙之谜，悬在人类文明征途上的一道道关隘，就像埃及金字塔前狮身人面怪物斯芬克司的考问，就像禅门宗师陷虎迷狮的重重“公案”，逼迫欲闯关夺路者交出答案。<br />
　　所有宇宙之谜中，最难解、最恼人，而对个人和社会又至为切近急迫的，是关于揭谜者自身的谜：人从哪里来？生命渊源于何处？人只是一架思维机器，还是具有所谓“灵魂”的半神灵？人在宇宙中的地位如何？人一死永灭，还是有来生后世？这些问题中，从生死之谜，尤其是死后有无续存的问题，关系到每个人最切身、最根本的利益，对它的解答，是人们决定人生态度、人生目标，建立人生观、价值观、伦理观的基石。正如《中国哲学发展史》所说：“人死后，是否还有某种‘生活’？如果没有，那么人生前的善恶还有多少意义？如果有，那么人生前的行为对死后有什么影响？这一切，都要落实到人生前应如何行动，应如何对待自己的一生？”（《魏晋南北朝》卷758页）广而言之，则社会、人类群体对这一问题的普遍解答，实为整个社会人类文明创立的出发点和基石。揭开生死之谜无疑成为人类智能面临的要务。从某种意义上讲，对生死之谜的破解，要比衣食住行条件的改善、工业新产品的发明等现实问题，更为现实，更为重要。<br />
　　实际上，早在人类文明发轫的初始，远祖们就开始破解人类自身的生死之谜，提出了为全社会所信受的答案。只要是理智健全的文明人，若没有对生死之谜的解答作舵和桨，大概很难使自己的生命之舟启航和保持平稳。从原始人到现代人，从僧侣修士、哲人智者、英雄领袖，到守财奴、酒鬼淫棍、江洋大盗，无不有各自对生死问题的答案。即使他不承思考过这个问题，这个问题也必然在他的潜意识中萦绕盘桓。<br />
　　人类破解自身生死之谜的途径，除了十九世纪德国著名生物学家海克尔所说研究灵魂本质的三种方法――自然科学的观察实验方法、发生进化史的方法、哲学思辨方法，还应加上宗教的方法，总共不出四种方法。其中自然科学的观察实验法和发生进化史的方法，到近代才显发达，自然科学的方法在研究物质现象方面虽然大显神通，成果斐然，但用来研究研究者自身时，却最显软弱无力，至今尚拿不出足以强迫人信服死后有无续存的确证。哲学思辨方法带有主观性、艺术性和学究气，从来异说纷纭，各执其是，难得举世共认的准衡和科技成果那样能令人信服的效应，何况哲学思辨总是少数具特殊禀赋的哲人智士的职事，不易普及于民间。自远古以来，被社会多数人乃至全体信奉认同，据以建立人生观、伦理观、价值<br />
观，作为全部生活支撑点的对自身生死之谜的解答，主要由各种宗教所提供。<br />
　　多数宗教，实际上都以人们内心深处的死亡焦虑为母胎，以对人生死之谜的解答为重要神学支柱，多宣扬人死后心灵进程并非终结，或灵魂永存，或轮回再生，行善者死后享福，作恶者死后受罪，以此创造劝善止恶的社会教化效益，解除人们对死亡的恐惧，调节和平衡社会心理。在以往漫长的历史进程中，此类观念为全球各地的绝大多数人所深信不疑，在各种社会意识形态中，对社会秩序的维持稳定，起着最为重大的作用。在今天的世界上，还有多半人信奉各种宗教，相信灵魂不死和轮回再生之说。<br />
　　宗教对人自身生死之谜的解答，或称作天降神授，或云乃古圣先知的训诫。虽然宗教理论家们也在用哲学思辨方法予以论证，但在多数宗教徒那里，大概主要是出于内心的宗教需要，作为一种绝对权威或国制习俗来信仰的。近代以来，自然科学突飞猛进，宇宙之谜被一个个揭开，基督教等的上帝创世说被推翻，天文学、生物学、医学、生理学等新知识的普及，使不少人自信人自身之谜已被揭破。宗教贬值，信仰转移于科学与金钱、确认人死永灭、无来生后世、无天堂地狱的人越来越多。随之而来的，是紧跟在科技飞速发展、财富急剧增长后面的人欲横流、道德退化、环境污染。<br />
　　其实，只要冷静考察自然科学对人自身认识的程度，便无理由妄称人自身之谜已全被揭破，无理由对人死后续存与否的问题作出决断的答案。人死永灭，仍然只是据尚不究竟的科学知识所作的哲学推论，确认其为真理，仍与宗教徒确认灵魂不死为真理一样，具有信仰主义的性质，无充足理由奉为科学结论。近现代的科学巨人，几乎无不承认科学对人自身认识浮浅，对灵魂、意识、死后有无续存的问题，持审慎态度。恩格斯在总结了当时自然科学关于生命现象的研究成果后说，生命最初怎样从无机物中产生的问题尚未解决，至于死亡， “或者是有机体的解体，除了那组成有机体实体的化学构成部分，再不留下什么东西；或者还留下某种生命的本原，即某种或多或少地和灵魂相同的东西。这种本原不仅比人，而且比一切活的有机体都活得更久。”（《自然辨证法・生与死》）当代诺贝尔奖获得者、著名脑电科学权威F・C・艾克尔斯，根据多年的研究成果，认为意识是先天本有的，大脑死亡后意识、自我是否以另外的形式复活并存在，“这是一个超越科学的问题，科学家应当忍住作出明确的否定的回答。”（《脑―― 精神问题是科学的前沿》1975年）<br />
　　最具挑战性的，是一类充斥古籍、超越时域而流传的与生死、灵魂有关，似乎在证明是灵魂不死、再生的特异现象，如记忆前世、活见鬼、脱体经验、濒死和死后复活体验、附体、借尸还魂等，并不因科学进步和人们不信此类现象而绝迹，总还是时有出现，在向人们透露出生死之谜的某种信息，因而总是有那么一些未必与宗教有关的人热衷于此类现象的研究，把它们列入心灵学、超心理学等研究课题。近几十年来，从各个角度对人自身的研究日益深入开拓，心灵学、超心理学研究是其中重要学科之一，在各大国家都设有专门机构。在中国，心灵学在三十多年中被列为禁区，判为迷信，直到随改革开放而来的气功热潮兴起，禁区才被逐渐冲开，气功和与其相关的特异功能，被列为钱学森教授称之为“人体科学”的主要研究课题。在气功效应和特异功能现象中，当然也少不了那种似乎与鬼神、轮回转世有关的事件和体验，促使一些探索者去试揭其谜底，并使人自然联想到传统宗教尤其是佛教关于轮回转世，诸天鬼神等的说法。<br />
　　在所有宗教中，以教义体系最为丰富深厚著称的佛教，与多数主要依感情需要仰赖神灵救赎的宗教不同，是以智能究明自身，以求解脱生死等痛苦为主旨，以“自净其心”、“如理作意”为超出生死的要道。佛教高张“了生死”之标帜，以如实认识自己为课题，对宇宙之谜，尤人自身身心、生死之谜，有至为明晰的解答。佛教以“缘起”的朴素辩证法观察生命现象，认为众生的存在是非断非常、即生即死而又因果、身心相续不断的无穷无尽的流转过程，人必有前生后世，死后必受生前行为的规定，轮回于天、人、鬼、畜、狱“五道”中，备受诸苦。在佛教看来，轮回过程中的生老病死等，乃至生命无常的本性，确实是苦，是生命的根本缺憾，如实认识生命秘奥，掌握超出生死轮回之道，自觉进行生命的变革进化，变生死流转、诸苦交攻的不圆满生命形态为“常乐我净”、永恒幸福、绝对自由的圆满生命，是人应有的奋斗目标和生命进化的终极归宿。佛经中，对人的身心结构、生死及生前死后的情状，天宫地狱、鬼神魔梵、诸佛菩萨罗汉，十方无量世界、无量国土，种种众生，描述至为详悉，展现了一幅极其广大壮阔、多层次、多结构的宇宙全景。佛教对生命现象、宇宙全景论述之广度、深度、明晰度，确非世间同类学说可与伦比。<br />
　　值得注意的是，佛教揭示生死之谜、宇宙之谜，除了用哲学思辨的方法进行论证外，据称主要依据的，是通过禅定修炼所开发出的超理性的天眼通、宿命通等神通智能的直观。佛教声称：佛祖释迦牟尼经过历劫精勤修道、勤苦探索，证得了圆满觉知宇宙实相的无所不知的大智能，揭破了种种宇宙之谜，发现并亲自证实了超出生死轮回之道，实现了生命的圆满变革，人只要肯依佛陀实践证明的道路修行，最终都能证同诸佛，无所不知，不生不死。<br />
　　佛教之说，固非科学结论，属于宗教范围，信受与否，悉由各人自由抉择，但不论佛教之说是否正确，它起码是人类认识自己进程中的重要思维成果，是东方传统文化最重要的组成部分，是人类文化及人体科学的宝贵遗产。从现代人的角度，重识佛学，继承精华，是当代人体科学等研究人自身的学科不容回避的课题。笔者专事佛教研究，多年来对生死轮回说特感兴趣，自身也曾有过一些与生死有关的特异体验，将众多佛典中有关生死轮回的说法作一番总结整理，提供给有关人士、人体科学研究者和对此类问题有兴趣的读者参考，是笔者义不容辞的职责。适有谢祥荣、严永奎二先生约请笔者写一本《生死与灵魂秘奥》的小册子，严永奎兄还热心提供有关资料，遂不顾病弱，欣然命笔。想不到题目太大，资料太多，尽管压缩，也还有二十七万言。这本书是以佛教轮回说为中心，对人类关于生死之谜的破解历程作一概略总结，以期启迪智能，开拓思路，唤起世人对认识自己的重视，有所裨益于精神文明的建设和人体科学的进展。<br />
　　读者朋友们：生死问题，的确是做人应予考虑的大事，以解决此问题为中心的佛家学说，自是一个文明人，尤其是生活在东方佛教文化圈内的人所应知晓的常识，于此无知，是为遗憾。但愿您跟着笔者的笔锋，对人类及佛教关于生死之谜的破解作一番浏览，就当作一次传统文化的旅游观光吧，也会使您大开眼界，增长见识，获得益处。<br />
　　倪维泉先生曾介绍了有关资料，严永奎、刘玉其、倪为国、索国云等同志的热心帮助此书出版，特致谢忱。<br />
<br />
　　陈兵　1993年12月于蓉城<br />
