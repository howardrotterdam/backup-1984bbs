																											<div id="postmessage_652570" class="t_msgfont">[版主一定要移动这个帖子也可以，不过我还是拒绝进入小黑屋。]<br />
<br />
人民报 by萧良量&nbsp; &nbsp;<br />
<br />
名誉对外商来说非常重要。连章子怡的绯闻都能使请她做广告的著名公司把合同转移到别人身上，使她蒙受巨大经济损失，更不要说那些请郭台铭加工电脑产品的世界著名厂商们，他们不可能不关注富士康的员工「自杀案」。<br />
<br />
实际上这些连环跳楼案和割腕自杀案是一场血腥的杀戮，是中方竞争对手为搞垮郭台铭、搞臭郭台铭，而有计划的、里应外合的、人为实施的谋杀案。<br />
<br />
要谈清这个问题，还得从关键人物郭台铭说起。<br />
<br />
●郭台铭个人简历<br />
<br />
1950年出生 <br />
<br />
1971年，台湾「中国海专」毕业，进入当时台湾前三大船务公司──复兴航运工作<br />
<br />
1974年，年仅24岁，成立鸿海塑料企业有限公司，资本额30万元。<br />
<br />
1985年，35岁，在美国成立分公司，创立品牌「FOXCONN」。<br />
<br />
2001年，51岁，创业27年时，获美国《福布斯》(Forbes)「全球亿万富翁」第198名<br />
<br />
2001年，鸿海以1442亿元台币营收，名列《天下杂志》台湾1000大民营企业龙头。<br />
<br />
2002年，郭台铭入选美国《商业周刊》评选的「亚洲之星」。<br />
<br />
爱好：每天至少工作15个小时。<br />
<br />
名言：领导者的睡眠时间不应该多于属下，他应该是第一个上班、最后一个下班的人。<br />
<br />
三不原则：不接受采访、不参加公开演说、不任意拍照。<br />
<br />
三无嗜好：一不爱名牌、二不开名车、三不注重打扮。<br />
<br />
● 中共官商无法理解的郭台铭<br />
<br />
中共官商是穿名牌、开名车、注重打扮，而且是「天上人间」那种地方的常客，他们认为经营和拉订单都是靠这么搞来的。<br />
<br />
而郭台铭一不爱名牌、二不开名车、三不注重打扮，自1974年以生产黑白电视零件起家后，36年来至今一年不曾休过三天以上的假。更不知道那种淫门朝哪面开。 <br />
<br />
从1974年创立鸿海的第一天起，24岁的郭台铭就目标很明确，即把公司发展成为台湾第一、亚洲第一、世界第一的企业。2001年营收1400多亿台币，2002年营收竟达2400多亿台币，这一年「鸿海精密」再次击败台积电、广达等龙头人马，蝉联为台湾股市的股王。现在鸿海精密工业公司几乎包办所有资讯科技类产品代工，并且已经超越「台积电」，成为台湾业绩最高的民营公司。 <br />
<br />
郭台铭说：「阿里山的神木（台湾著名的风景之一）之所以大，4000年前种子掉到土里时就已决定了，决不是4000年后才知道的。」<br />
<br />
● 当天让康柏的图纸变成模型<br />
<br />
富士康（Foxconn）这个牌子已经打出去了，很多人选择电脑的一个主要因素就是看主板是不是富士康原件。世界知名的PC品牌机中，使用富士康产品的数不胜数，如著名的Dell、HP（包括Compaq）、IBM……。<br />
<br />
郭台铭的事业是创业打拼出来的，连外国企业也不能不服。例如，1987年，37岁那年，他在美国成立分公司创立品牌「FOXCONN」已经二年。为了拿到美国康柏公司的订单，他抛下董事长的身份，每天像个销售员一样拎着公文包去卖连接器，但外国人十分谨慎，没有百分之百的质量保证，不会轻易与原来的合作者分手。在接连吃闭门羹的情况下，郭台铭并没有灰心，他在康柏总部旁建了一个成型机厂，只要康柏有新的设计，最快当天郭台铭的公司就能让康柏的图纸变成模型，这种惊人的加工速度和质量保证，没有任何一家可比，把康柏公司惊到目瞪口呆。双方的合作大门就是这样开启的，这一做就做下去了。<br />
<br />
PS2，戴尔电脑和Intel的主板，康柏电脑，都是鸿海的代工产品。鸿海凭借为这些厂商代工，登上股王之位。鸿海的客户不希望他们的零件商锋头过健。幸好郭台铭也是个喜欢低调拓展事业的人，他尽量回避媒体。就连在台湾的公司总部，郭台铭都刻意远离科技大厂聚集的新竹科学工业园区，选在较不为人知的土城工业区。<br />
<br />
1988年，富士康企业集团成为在大陆投资兴办的专业生产电脑接插件、精密零组件、机内线揽、精密模具及电脑整机的高科技企业集团。至1999年底，富士康集团分别在深圳和昆山建成两大资讯科技工业园。其中，在深圳的公司占地1600余亩，预计在深圳的投资将超过20亿美元，加上昆山基地，以达成全球最大的电脑整机和零组件生产企业集团及全球个人低价电脑的主要制造基地的经营目标，现在鸿海已经成为大陆第二大出口商。<br />
<br />
● 郭台铭一生中做出的最错误决定<br />
<br />
郭台铭是台湾科技首富，是台湾排名第三的富豪。2008年《富比世》杂志估计，郭台铭财产总值达60亿美元，约合1800亿元台币。<br />
<br />
在中国大陆花巨资建成了科技工业园的「硬件」，把这里成为全球最大的电脑整机和零组件生产企业集团及全球个人低价电脑的主要制造基地，这是郭台铭一生中做出的最错误的决定。<br />
<br />
这个决定源于他只辛勤的工作，却不知道共产党是不与任何人合作的，是不允许任何人在它眼皮底下吃肉的，更何况是在它的地盘里。<br />
<br />
● 富豪碰到共产党的教训<br />
<br />
当初为搞垮台湾经济，江泽民指示有关主管部门，「要不惜一切代价将王永庆的资源拉过来」。为此江让他的儿子江绵恒去拉拢王永庆的儿子王文洋，尽管王文洋因婚外情与父亲关系紧张，但还是成功作为说客，让王永庆动了心。<br />
<br />
2000年11月23日，台湾首富、台塑集团董事长王永庆率领八名台湾工商界领袖公开给总统施压，呼吁台湾政府改变当前「政治挂帅」的不正常现象，回归「经济优先」的局面，方便台商在中国大陆，香港投资。一番折腾之后，王永庆如愿以偿将巨资投到了大陆。 <br />
<br />
一年后，2001年11月1日，被江「请君入瓮」「关门打狗」的王永庆不再打肿脸充胖子，首度坦承，当年巨额投资漳州后石电厂是一项错误的投资策略，他说目前漳州后石电厂已经出现亏损。一年将损失近31亿人民币的营收。福建当局以电力「供过于求」为由，强迫漳州后石电厂的电力打6折，瓮中之鳖的王永庆迫于无奈，只得答应。幸好江泽民的性子太急，到王永庆去世时，他的子女妻妾们还有好大一笔家产可以闹，让看客大松了一口气。<br />
<br />
香港红色资本家、中共全国政协副主席霍英东，到2002年他在广东的港口公司已经经营了10年。2002年3月，地方当局通知他交出70%的公司收入。在全国政协公开会议上，这位副主席愤怒抨击地方官员无端勒索他的企业，还派武警到码头上，以调查为名，向他恫吓施压。他感到这对他简直是「晴天霹雳」，可见中共是否拿香港红色资本家当回事。<br />
<br />
其实这种霹雳从1949年10月中共建政以来，对中国人民就没停止过。所有的资本家企业家，不管有多大产业，统统收为共产党所有，美其名曰是收为「国有」。<br />
<br />
北京沦陷不久，一位非常知名的企业家，因为在北京有一栋让中共高官垂涎三尺的王府般气势的豪宅，一天晚上被汽车接走说是请吃饭，从此再没有回家，家里去要人，刚开始说没这回事，后来说他是汉奸被处决了，于是全家以「汉奸家属」被扫地出门。那位人民高级「公仆」终于如愿以偿的搬了进去。这种冤案不知道有多少。<br />
<br />
● 中共盗窃外资高级技术 <br />
<br />
为了成为世界科技强国，中共采取拉进来打出去的办法。<br />
<br />
打出去，就是派遣那些成绩优异的学生去留学，这些技术特务的任务就是念好书，毕业后可以轻而易举的进入世界级大公司核心技术部门工作，然后把那些技术偷窃出来。还有的是已经在世界级大公司核心技术部门工作，被中共收买的。近年来，时常看到这种新闻，某某被发现为中共盗窃技术资料而被逮捕。这些人被捕后，中共马上撇清关系，他们的一生就交代了，此时他们才发现自己没有了利用价值的悲哀。<br />
<br />
拉进来，就是把世界级大公司请到中国办厂，里面派上自己的科技人员，等技术拿到手了，开始跟你找麻烦，罚你的款，跟你打官司，最后搞到你焦头烂额，还说这是「民意」，直到你忍痛割爱，把投资的硬件都放弃，转移到其它国家去。它中共就拿着你的技术和你装修好的工厂开始抢你的订单、抢你的生意。这些年不时会看到一些大公司撤资。<br />
<br />
● 富士康碰到了和谷歌同样的麻烦<br />
<br />
谷歌是进驻中国大陆后挺「自律」的世界级引擎搜寻公司，没消停几天，中共开始没结没完的找谷歌的麻烦，总是说「有人举报」，说谷歌上可以搜寻到黄色网站，后又找几个作家说自己的文章出现在谷歌，涉及侵权，提出告诉，要求赔偿。直到最后谷歌忍无可忍说出自己不断被折腾的原因是， 中共一直对中国人权活动人士的Gmail帐户进行攻击，想查看他们的邮件内容，谷歌没有配合。中共的另一种攻击是工业间谍活动，目地是从目标公司收集有价值的知识财产，这波及了20家著名外国大企业公司，而大部分公司并不知道他们被攻击。最后谷歌决定信守「不作恶」的原则，撤到香港去了。<br />
<br />
现在，郭台铭碰到了和谷歌同样的麻烦，在深圳富士康，从今年1月23日至5月26日，4个月内12起员工坠楼或刀砍，或伤或亡。郭台铭来到深圳，并于5月26日在富士康龙华园区会见200多名海内外媒体记者，并主动带记者参观社区、厂区、车间、宿舍楼、员工关爱中心等。为避免伤亡再次发生，近日，富士康在宿舍楼的阳台安装防护网，吊车进入富士康担负起吊任务。 <br />
<br />
防护网做好了，郭台铭还没离开深圳，5月29日晚6时许，富士康公司深圳厂房发生蹊跷火灾，事件中至少逾10名工人受伤，另有一名消防员不适。有员工说，以后要有火灾，跑都不容易了。 <br />
<br />
一位当地公安局的头头说；郭台铭能把生意搞那么大，怎么连这点事还看不明白，这不就是让他滚蛋嘛！他一个台湾人要想在咱们地盘上混，不容易，混不出人样来，让人看不起，闹的太红火了，哪个不想吞下去啊！这回的来头大了！<br />
<br />
● 郭台铭的大陆对手「全国新首富」王传福发迹没有脚印<br />
<br />
提到多年后才跳槽到比亚迪公司的富士康高管内鬼，必须先提到一个经不起推敲的「全国新首富」王传福。看到王传福的发迹史，让人不得不想起上海滩曾富甲天下的首富周正毅。他们的发迹史都很类似，都是跳跃式的，无法连贯，也无法说清楚，反正钱是到手了，突然成了首富。 <br />
<br />
综合网上王传福的简历：从不名一文的农家子弟到身家亿万的集团公司总裁。1966年出生在中国安徽省无为县。教育程度硕士。1995年2月，27岁的王传福辞去公职，向表哥（哪个表哥？）借了250万元钱后，领着20个人在深圳莲塘的一个旧车间敲敲打打，成立了日后风光无限的比亚迪科技有限公司。他从一开始就把目光投向技术含量最高利润也最丰厚的电芯生产，这为其以后的成功奠定了基础。2003年1月，比亚迪收购秦川汽车有限责任公司，任董事长，四月公司正式更名为比亚迪汽车有限公司。<br />
<br />
是什么力量把王传福推向了「成功」的巅峰呢？别人还真模仿不了。只知道当年和江绵恒混在一起的上海首富周正毅都是从银行拿来钱，分给上海市委书记陈良宇等高官们。银行行长兜着这些坏帐，最后进了监狱。有人曾这样形容受黄光裕面试的情景：感觉在黄光裕的犀利目光下，自己就象是一条狗。黄光裕被拘捕之后，兜出来很多事。不和上面勾着、分赃，在中国大陆，你是一事无成。<br />
<br />
在2008年的胡润百富榜上，王传福尚以60亿元的身家排在第22位，而2009年却一下子飙升到了榜首。2009年9月28日，胡润公布了“2009胡润百富榜”的前两名，比亚迪总裁王传福以财富 350亿元成为中国首富。<br />
<br />
一个没有背景的农家子弟王传福能够迅速成为中国首富，这可不是件好事。周正毅可是蹲了两次监狱。黄光裕也被判刑罚巨款。<br />
<br />
● 富士康高管早已成了王传福的内鬼<br />
<br />
王传福是用什么方法飙成中国首富的，也就是那么几条道儿，新华网、人民网上面每抓一个「首富」，每介绍一次，在这里就不重复了。但王传福收买富士康高管做内鬼，偷窃富士康技术，证据确凿，实在不光彩。郭台铭多次公开指责比亚迪剽窃商业机密。<br />
<br />
原来在富士康担任主管的柳相军，和负责编制工艺流程文件的司少青，二人于2008年7月从富士康跳槽比亚迪。<br />
<br />
但，柳相军于2005年5月底申请自富士康离职，却早在其申请离职前，就已经拥有并使用比亚迪的电子邮箱，而且与比亚迪的行政总裁李柯进行重要的工作讨论，比亚迪负责人直接参与侵犯富士康商业秘密。<br />
<br />
从2005年6月10日，柳相军发给比亚迪负责人王传福的邮件中，证明柳相军对王传福负责，并且将获取的「富士康手机一季度的指标状况」等商业秘密，直接寄给王传福。比亚迪董事长王传福通过柳相军等人，以不正当手段获取富士康商业秘密。<br />
<br />
富士康在2006年5月向公安机关报案。直到2008年2月才开庭，在深圳法院对柳相军案庭审中，富士康诉讼代理人对柳相军提问：「在比亚迪领导层中，你向谁负责？」柳相军回答：「王传福。」<br />
<br />
● 股神巴菲特得知王传福背后的靠山是谁<br />
<br />
在波克夏股东会举行的同时，鸿海董事长郭台铭在台湾接受媒体专访，就竞争对手比亚迪偷窃商业机密一事，隔海对股神巴菲特提出三个问题：一、为何投资偷窃商业机密的公司？二、敢不敢开比亚迪汽车上、下班？三、国际大厂油电混合车领域很久才开始赚钱，以何种专业知识判断比亚迪的潜力？ <br />
<br />
郭台铭把问题丢给巴菲特，并将焦点锁定在比亚迪的专业水平及道德问题。很多人认为比亚迪电池的功能并不好，有其它更好的公司，但巴菲特仍力挺王传福，他说「比亚迪董事长王传福才是真正的明星，同时比亚迪定会成为中国最著名的公司。」巴菲特泄露了他得知王传福背后的靠山是谁，巴菲特拍的不是王传福的马屁，而是拍他主子的马屁。 <br />
<br />
巴菲特在中央电视台专访中还说：「中国有一个很棒的未来，中国的发展，就像一个人的成长和释放出潜能，中国正在成长，还会不断地更加成长，它只是刚刚开始，我会告诉所有中国人，你们都很幸运。」巴菲特明确否定了何祚庥的话：「谁让你不幸生在了中国！」<br />
<br />
除最早投资的中石油，还有去年加码的比亚迪之外，股神是否还继续投资中国官方企业，巴菲特的回答也是相当溜须讨好中共的。 <br />
<br />
● 巴菲特投资中共而不是王传福<br />
<br />
郭台铭从1988年使富士康企业集团成为在大陆投资兴办的专业生产电脑接插件、精密零组件、机内线揽、精密模具及电脑整机的高科技企业集团。至1999年底已经在深圳占地1600余亩。22年来，鸿海早已成为大陆第二大出口商。<br />
<br />
巴菲特之所以被称作「股神」，是因为他投资准确，获利丰厚，没失过手。那么正像郭台铭对他的三问一样，当巴菲特完全了解王传福当董事长的比亚迪偷窃富士康商业机密，电池质量无法与其它公司的相比，制造的汽车保证不了质量， 那么是什么原因让巴菲特再追加投资在王传福公司，而不是撤股呢？<br />
<br />
巴菲特说「王传福才是真正的明星」，「比亚迪定会成为中国最著名的公司」。在中共一党统治的中国，党今天可以让王传福当明星，明天可以让他成为阶下囚；党今天可以让比亚迪公司做大，明天可以给它贴封条，这已经是不争的常识。所以，巴菲特投资的不是「王传福」的公司，而是把钱投资给中共。<br />
<br />
既然比亚迪是中共的公司，王传福不过是出台唱戏的皮影儿，那么富士康控告比亚迪偷窃商业机密，等于是在控告王传福的主子，在人家地盘里的郭台铭能不有麻烦吗？富士康连环跳，与谷歌撤出前的情形没有什么区别，和中国50年代对资本家的「社会主义改造」「打老虎」「三反五反」也没有什么区别，只不过年代不同了。<br />
<br />
● 周永康的影子在晃动<br />
<br />
在深圳富士康的保安中，有一部分是中共公安系统的人，他们拿着两份薪水。原富士康员工透露：坠楼者是他杀和被迫自杀。<br />
<br />
一个富士康后勤部门工作的人，在网络上多处发贴披露说富士康中坠楼的人中，据他知道就有数位被保安（环保科）打死的。他还说，凡是被叫进环安课的，没有不被暴打，侮辱的，不亚于日本人当年。特别是环安课课长顾钦明和他手下爪年刘锋，一点人性都没有。<br />
<br />
他说：「其中一个跳楼的人，是因为年纪小偷拿了公司一个小电子产品，活活被保安打死从楼上扔下来，此死者当时左胸有一个血洞，是被保安用钢管戳穿肋骨致死。」<br />
<br />
「还有一个因为和主管不和（原因是年轻人工作中损耗品太多被主管骂的太凶顶撞而已），此人在离职前一周左右被主管栽赃，被环安科关起来，其主管和保安用钻头活活钻死从楼上扔下来。」<br />
<br />
「另外哈工大名牌毕业生的那位哥们（孙某）本科毕业新干班新员工被环安课侮辱，殴打，非法搜查，非法拘禁，是被某人（具体不清楚和什么人结缘）栽赃，后来因为经不起此等折磨侮辱选择跳楼」<br />
<br />
「5月13号的安徽男不用说就知道肯定不是自杀了，自杀的人不会自己捅自己4 刀，再去跳楼，而且翻过1.5米的围墙。」<br />
<br />
5月14日晚深圳富士康发生的今年第9起坠楼事件，是安徽籍员工梁超坠楼。富士康媒体办负责人对外通报，警方经现场调查，在事发现场发现一把带血的匕首，死者身上有4处刀伤，在宿舍7楼楼顶发现有血迹。但深圳警方认定为自杀。<br />
<br />
郭台铭在中国大陆投资设厂，给中国带来极大利润，建厂22年来，到了2010年富士康突然集中发生血案，公安却没有一点动静。中共只报导又有人跳楼了，却没有报导后续公安做了什么。报导中指责富士康是血汗工厂，却不报导那血是黑红两道给扎出来的，反而在5月30日新华图片中出了一个《富士康老板郭台铭背后的女人们》，给郭台铭制造绯闻。这就不是卧底富士康的公安手能伸到的地方。<br />
<br />
富士康连环跳命案、割腕案、纵火案，是中共高层掠夺台湾富豪的丑闻。什么两岸双赢，什么用经济先行达到两岸和平统一，都是永远不可能实现的幻觉。△</div>

							
							
							
							
