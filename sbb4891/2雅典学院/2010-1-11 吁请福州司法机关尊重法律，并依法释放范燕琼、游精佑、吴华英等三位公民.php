<br />
 <br />
<br />
&nbsp; &nbsp;“他是我的父亲！”我永远都会这么骄傲地说出口----从他被犯罪那天起，我有资本这么骄傲。“他是我的父亲，他叫游精佑！”<br />
<br />
&nbsp; &nbsp; 2009年七月五日，我的父亲游精佑被涉嫌诽谤罪，被福州市马尾公安局刑拘。在七月的最后一天，又被“诬告陷害罪”的名目，被逮捕。父亲从小就教导我，做人要讲良心。我怎么也不会想到，正义，善良，顾家，孝顺的父亲，会摊上一个所谓“诬告陷害”的罪名。<br />
<br />
在这么一个台风肆虐的天气，我想念我高墙里的父亲。不知道他能否知道家人的思念，不知道他能否知道家人四处奔走希望正义在这个冷漠的世界里幸运地被我们遇见，不知道他能不能吃得饱睡得好。<br />
<br />
&nbsp; &nbsp; 父亲在墙里头，不明就里；我在外头，黯然神伤。<br />
<br />
&nbsp; &nbsp; 一想到父亲，眼泪就不自觉流出眼眶。父亲是个理性但偶尔感性的男人。从小就不是太听话的我，叛逆与不理解，吵嚷和愤怒是我大多数时候对于父亲的态度。随着年岁渐长，才慢慢懂事，才开始慢慢知道父亲抚养教育我长大背后的艰辛和难过。在我最不懂事的时候父亲听到别的父母夸耀自己孩子时自己黯然的眼神；而父亲总是把我的照片放在钱包里，时不时拿给别人看：喏，这是我的女儿。现在这些表情在我脑海里显得如此深刻，犹如佛祖拈花，而我，想笑，更想哭。<br />
<br />
&nbsp; &nbsp; 父亲未被逮捕前我们一家人的最后一次聚餐----可笑的是，我们一家人的团聚，往往不是在福州我们的家，而是父亲的工地。----结束后我们一家人顺着月光往回走。爸爸突然说：如果我被捕，请你们给我找律师。妈妈和我震惊！我情绪激动地大声质问他：怎么可能！到家时我还在愤愤不平的情绪中，那时候的我大声地对着父亲吼着：你要这样到什么地步，你预备为了你的正义抛下我们一家人不管不顾吗？！现在的我后悔了，我幼稚，单纯。我为了爸爸的遭遇而愤怒，爸爸则是为了千千万万个爸爸这样的遭遇在抱不平。见得多了，也就麻木了，麻木到可以漠然对待自己的“莫须有”。<br />
<br />
&nbsp; &nbsp; 道别父亲，我和妈妈先回福州。父亲送我们上车，我拍着父亲的肩膀说我在福州等你。爸爸凝视着我，勉强说了声好。我深深地看了一眼我的父亲----我不知道这将是不知期限的最近一眼。<br />
<br />
&nbsp; &nbsp; 七月五号下午六时许，我和妈妈拨打爸爸的电话，就一直是无人接听的状态。焦急，心慌的我们只是静静地等着，然后找一些理由来安慰自己，也许他是在检查工地，手机没信号呢。晚上十一时左右，无情的警察们打破了我们的幻想。在未出示搜查证的情况下带着父亲到家里来拍了很多照片，然后就把父亲带走了。父亲走时说的最后一句话是：没事的。<br />
<br />
&nbsp; &nbsp; 父亲总是对我说：没事的。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;很快，父亲的这句话被否定了。马尾公安局说此案是涉及国家机密的案件，拒绝记者采访。我和吴华英阿姨的女儿及范燕琼阿姨的女儿几次去马尾公安局讨一个说法，都被忽悠过去了。往后看，警方忽悠的不仅是我们而已。警方说父亲及其他两个阿姨涉嫌的是诽谤罪，但却因案件调查需要不能告诉我们如此“国家机密”。面对如此无厘头的事，我们选择了冷笑，与等待。<br />
<br />
&nbsp; &nbsp; 七月三十一号，家里收到了逮捕证。上面白纸黑字写着，游精佑因诬告陷害罪已经被逮捕。此罪名甚至比诽谤罪更不能让我们理解，但是家人还是慌了，乱了。人与官斗，如何斗得过啊！<br />
<br />
&nbsp; &nbsp; 在这样的一个台风肆虐的双休日，办你的警察们也许一家人其乐融融地在家里泡着茶避着风。而我的父亲却因为正义，因为积极地帮助别人而被当作鱼肉，那么刀俎是谁？<br />
<br />
&nbsp; &nbsp; 在这样的天气里，我想念你，父亲。但是你何时回家来？我只想近距离看你一眼，看看你换上拖鞋，喝一口茶那种满足的样子。我想告诉你一句话----不再是指责你自私不顾家----告诉你我会努力考上一所法律学校。你没说，但你做给我看了：正义从来不是私有品，而是公共财产。</div>

							
							
							
							
