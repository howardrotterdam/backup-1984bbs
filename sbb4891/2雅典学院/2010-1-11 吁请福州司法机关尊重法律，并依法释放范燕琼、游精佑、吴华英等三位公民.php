													<h2>维权者悲惨命运，见证法治的艰难</h2>
						
						
																											<div id="postmessage_431482" class="t_msgfont">(2009-07-02 21:58:59) <br />
标签：杂谈&nbsp;&nbsp;分类：福建网民诬告陷害案 <br />
&nbsp; &nbsp; 昨天，我在艾未未处参加“休闲不上网”活动时，收到了一个网友发来的短信。信中说，吴华英女士在福建省福清市的家中被抓获了。<br />
<br />
&nbsp; &nbsp; 看了这条短信后，我的心情顿时沉重起来。认识吴女士是一年前的事情，她为了弟弟的一起刑事冤案，多年来尽管一直在坚持上访，但还是得不到任何结果。通过别人的介绍，她给我递交了厚厚的申诉材料。对她弟弟的案件，我也感到无能为力，只好写了一篇博文为之呼吁。<br />
<br />
&nbsp; &nbsp; 想不到的是，吴女士多年的努力不仅没有救出弟弟，反而把自己送进了看守所，也许还要面临牢狱之灾。<br />
<br />
&nbsp; &nbsp;为了弟弟案件的平反，吴女士家庭也早已破裂。这些年来，都是她一个人奔波于北京与福建在上访。她的上访不仅没有感动司法机关，反而还屡遭截访与和压制。<br />
<br />
&nbsp; &nbsp; 她的弟弟在2001年福清市纪律检查委员会爆炸案中抓获后，至今已在看守所羁押了八年是时，二审法院仍然没有作出终审判决。吴女士弟弟的案件，曾经引起全国人大常委会的关注，可福建省司法机关就是顶着不予纠正。如今上诉已经长达两年多，也不见作出终审判决（一审判处死缓，上诉发回重审后再判死缓。第二次上诉后，至今长达两年多不判决）。<br />
<br />
&nbsp; &nbsp; 吴女士与她弟弟的悲惨遭遇，相信绝大多数民众都会为之唏嘘不已。写到此，我又想起了访民朱承志老先生的遭遇。5月下旬时，美国的佩洛西老娘们来了中国，一些访民就去国新办门口，朱承志也去凑了热闹。他既没有打横幅，也没有散传单，更没有喊口号，只是站在外面拍摄了一些照片。“电饭煲”发现他后，朱承志与人家较劲了几句，就被警察带到了派出所。先是行政拘留了十天，后又以涉嫌聚众扰乱社会秩序犯罪而遭刑拘。朱承志说，国新办门前的监控录像可以证实，自己只是在外面拍摄了照片而已，根本没有参与聚众扰乱社会秩序的行为，但任凭你怎么辩解也起不到作用了。<br />
<br />
&nbsp; &nbsp;由吴华英、朱承志的被抓，让我想起了福建维权者李祥谋的被抓。“海难”造成了十三名中国人死亡，至今没有哪个部门去查处这起事故，反而将代表死难者家属讨说法的李祥谋抓获。这个世道到底还有没有起码的公平与公义呢？<br />
<br />
&nbsp;&nbsp;吴华英、朱承志、李祥谋、纪斯尊，还有千千万万的维权者，他们悲惨命运见证了法制进步的艰难。但我始终相信，中国社会最终还是要走向民主与法制，这是任何人、任何力量都主宰不了的。这正如孙中山先生所说的，世界潮流浩浩荡荡，顺之者昌，逆之者亡。是到了该警醒的时候了！</div>

							
							
							
							
