													<h2>吁请福州司法机关尊重法律，并依法释放范燕琼、游精佑、吴华英等三位公民</h2>
						
						
																											<div id="postmessage_425902" class="t_msgfont">(更新：附今日签名名单） <br />
2010-01-11 22:56 | (分类:默认分类)<br />
 原文链接：<a href="http://fjwangmin.72pines.com/archives/30" target="_blank">http://fjwangmin.72pines.com/archives/30</a><br />
<br />
<br />
致福州市马尾区人民法院、马尾区人民检察院、马尾区公安局及有关部门：<br />
<br />
2010年1月11日，范燕琼、游精佑、吴华英等三位公民被非法超期羁押已进入第四天，特此吁请福州司法机关尊重法律并依法释放范燕琼、游精佑、吴华英等三位公民。<br />
<br />
2008年2月10日，福建省闽清县女青年严晓玲离奇死亡。严母林秀英为弄清女儿死因多方奔走上访一年多，皆未获得一个令其信服的结果。范燕琼、游 精佑、吴华英等八位公民，因以代笔发帖、制作视频等方式帮助林秀英在网络上表达对严晓玲死因的质疑，先后于2009年6月26日及其后两周内被马尾警方抓 捕。其后在社会舆论的压力下，多位公民以取保候审的方式获得释放，但范、游、吴三人仍遭羁押。<br />
<br />
2009年11月11日，所谓福建网民“诬告陷害”案在福建省福州市马尾区法院一审开庭。范燕琼、游精佑、吴华英等三网民被马尾区检察院以诬告陷害 罪起诉。此案本应于2010年1月7日前作出最后答复。但如今最后期限已过，马尾区法院仍未宣判，警方也未释放三人，此举已构成非法超期羁押。法院还于日 前拒绝病情严重的范燕琼女士保外就医的申请，同时第三次拒绝范、游、吴三人取保候审的申请。<br />
<br />
我们认为：<br />
<br />
1. 范燕琼、游精佑、吴华英三人的行为不构成诬告陷害罪。三人或代笔或制作视频，都只是转述林秀英的观点，既未捏造事实，也未向任何国家机关进行告发（亦不曾 有国家机关受理过“告发”），更不存在主观上欲使他人受到刑事追究的故意。退一万步讲，纵然范、游、吴三人所撰网帖与所发视频与严案真相有所出入，但其本 意是为帮助弱者林秀英，亦未对聂志雄等三人造成严重的生命、财产损失，本案充其量只是范、游、吴三人与聂志雄等之间关于名誉侵权的民事纠纷而已。<br />
<br />
2. 福州司法机关自严案以来，屡屡违背法律程序。关于严晓玲案，闽清警方至今未给公众一个令人信服的结果；马尾警方仅因言论便抓捕多位公民，引起舆论哗然；马 尾区法院在一审庭审时更是公然违反刑事诉讼法，在同案人林秀英抵达马尾区法院要求出庭作证、范游吴三被告及律师们要求法庭批准林秀英出庭作证的情况下，将 林秀英阻挡在法庭门外。如今，范、游、吴案既未宣判，福州司法机关非法超期羁押三位公民，已经严重地践踏了刑事诉讼法。<br />
<br />
3. 范燕琼女士身体状况严重恶化，11月庭审时已不能站立行走，靠轮椅才勉强出庭，12月一直在挂瓶，感冒、呕吐，左肾绞痛。且范有疾病史，据福建省立医院病 历记载，范曾因多发性动脉炎、肾性高血压等在福建省立医院动过手术。辩护律师、范燕琼的女儿、福州市第二看守所先后为其取保候审、保外就医未果。法院早就 应该变更对范燕琼的强制措施，予以保外就医。现案件已严重超审限、超期羁押，我们担心被告人范燕琼出现不测，造成难以弥补后果。<br />
<br />
我们要求：<br />
<br />
1. 福州警方应立即释放范燕琼、游精佑、吴华英等三位公民，并对此前以取保候审方式释放的多位公民解除取保候审，或变更强制措施。<br />
2. 范燕琼女士的身体状况严重恶化，福州司法机关须保证其生命安全及健康。相关责任人须为造成范燕琼女士病情恶化负法律责任。<br />
<br />
我们提出此呼吁，是希望福州的司法机关能够以法律为依据、以事实为准绳处理此案。同时也请求有关部门从人道主义出发，让此案有一个圆满的结果，让范燕琼、游精佑、吴华英三人能尽早与家人团聚，让病重的范燕琼女士尽早就医并恢复健康。<br />
<br />
此致。切盼。<br />
