													<h2>【福建网民诬告陷害案】又被延期一个月审理！（转至刘晓原博客）</h2>
						
						
																											<div id="postmessage_428161" class="t_msgfont">2010-01-12 23:12 | (分类:默认分类)<br />
2009年12月30日，我在马尾区人民法院见到福建网民诬告陷害案审判长吴法官，他告诉我说，这起案件审限在一个半月的基础上，经向福建省高级人民法院申请已延长了一个月，审限在2010年1月7日期满。<br />
<br />
&nbsp; &nbsp;但两个半月审限到期后，也不见法院作出判决。我心想，是不是检察院撤回了起诉呢？<br />
<br />
&nbsp; &nbsp;审限过后，我多次给马尾区法院刑事审判庭打电话，不但没能找到吴法官，而且连本案书记员也找不到。<br />
<br />
&nbsp; &nbsp;今天上午，游精佑的哥哥给我打来电话，他说，昨天福建吴律师去会见了游精佑，从看守所了解到羁押期限又延长了一个月。挂了游精佑哥哥打来的电话，我立即拨打马尾区人民法院刑事审判庭电话，还是没能找到本案法官和书记员。<br />
<br />
&nbsp; &nbsp;但凭我的判断，案件应是退回检察院侦充侦查了。于是，我给马尾区人民检察院打了电话，找到了本案代理检察员陈检察官。<br />
<br />
&nbsp; &nbsp;陈检察官说，前些天，马尾区人民法院要求检察院补充一些证据，检察院遂按照《刑事诉讼法》第165条规定，提出了延期审理的建议。陈检察官反复强调，这不是退回补充侦查，而是应法院的要求，检察院才建议延期审理。<br />
<br />
&nbsp;&nbsp;《刑事诉讼法》第165条第二项规定，在法庭审判过程中，遇有下列情形之一，影响审判进行的，可以延期审理：（一）需要通知新的证人到庭，调取新的物证，重新鉴定或者勘验的；（二）检察人员发现提起公诉的案件需要补充侦查，提出建议的；（三）由于当事人申请回避而不能进行审判的。<br />
<br />
&nbsp; &nbsp; 按照《刑事诉讼法》第165条第二项规定，只有检察院人员发现公诉的案件需要补充侦查，才能提出延期审理的建议。<br />
<br />
&nbsp; &nbsp;我不明白的是，为何法院会主动要求检察院提出补充侦查？为何在一个半月审限届满时，不要求检察院补充侦查？而要等到审限再延长一个月，在用尽了两个半月的审限后，才会想到证据不足要补充侦查的问题？<br />
<br />
&nbsp; &nbsp; 这是由两篇文章、一个视频引发的案件，案情其实很简单，按照《刑事诉讼法》168条规定，最长审限就是一个半月。但法院却把案件复杂化、疑难化，不管本案是否符合条件也适用二个半月审限。在二个半月审限届满时，无法再拖延审限了，就想到由检察院提出补充侦查建议。想尽一切办法拖延审限，马尾区人民法院有滥用职权之嫌。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;这起简单的案件，公安机关抓了八个人，调查五十七个证人，连QQ聊天和电子邮件都调取了，整了十七大本证据，我不知检察院还能补充侦查到什么证据？<br />
<br />
&nbsp; &nbsp;马尾区人民法院对案件审理了两个半月，在审限无法再延长时，如发现检察院证据不足的问题。那么，马尾区人民法院就应按照《刑事诉讼法》第162条第三项规定，作出证据不足，指控的犯罪不能成立的无罪判决。<br />
<br />
&nbsp; &nbsp; 马尾区人民法院现在的做法，很显然是怕担责任，而找借口拖延审限。当然，也有可能是官员们还没有对案件下指示，法院不知该如何来判决，而审限又已经到了，只好找借口去拖延。<br />
<br />
&nbsp; &nbsp; 最高人民法院、最高人民检察院、公安部《关于严格执行刑事诉讼法，切实纠防超期羁押的通知》(法[2003]163号)规定，凡不符合刑事诉讼法关于重新计算犯罪嫌疑人、被告人羁押期限规定的，不得重新计算羁押期限。严禁滥用退回补充侦查、撤回起诉、改变管辖等方式变相超期羁押犯罪嫌疑人、被告人。<br />
<br />
&nbsp; &nbsp; 陈检察官表示，如人民法院审理违反了法律规定，律师可以向人民检察院举报控告。但我以为，律师的控告会有用吗？这起案件超审限和超期羁押问题，难道公检法三家会不知道吗？<br />
<br />
&nbsp; &nbsp; 案件退回检察院补充侦查后，按照《刑事诉讼法》第166条规定，人民检察院应当在一个月内补充侦查完毕。按照最高法院对《刑事诉讼法》的司法解释第157条规定，建议延期审理的次数以两次为限。<br />
<br />
&nbsp; &nbsp; 由于退回补充侦查了，按照《刑事诉讼法》第168条第三款规定，案件的审限将要重新计算，即从人民检察院补充侦查完毕移送人民法院开始计算。<br />
<br />
&nbsp; &nbsp; 这样一来，等到一个月补充侦查期满后，人民法院对案件的审限可以是一个半月之久，如向省高级人民法院申请的话，还可以再延长一个月，如此下来又是两个半月的审限。到时，还要继续玩下去的话，检察院可第二次提出补充侦查建议，再折腾玩一个来回了。<br />
