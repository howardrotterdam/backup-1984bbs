																											<div id="postmessage_429271" class="t_msgfont">文章原载：<a href="http://www.my1510.cn/article.php?id=b874a5c4e31f2eb8" target="_blank">http://www.my1510.cn/article.php?id=b874a5c4e31f2eb8</a><br />
<br />
您好：<br />
<br />
福建省省委书记孙春兰女士，但是我还是想唤您一声阿姨。为您是我的长辈，为我对您存在的期盼。<br />
<br />
我 是福建省最近一出奇案：因一片视频和一篇文章导致的三网民“诬告陷害案”被告之一游精佑的女儿。爸爸因同情为女儿讨一个真相的林秀英，替林秀英女士录了一 段口述视频，忠实地记录了林秀英女士哭诉的整个过程，且在视频末尾加上了“此视频忠实记录讲述者的倾诉不代表制作人的立场。”这样的字眼。哪料却被马尾区 公安局在7月5日拘留，而后以诽谤罪将其与其他两位阿姨逮捕，且以涉及国家机密为由拒绝律师的会见。此案已经过了一审，却迟迟没有得到审判的结果。<br />
<br />
孙 阿姨，我相信您一定也是个母亲，您了解家人之间，父女之间那种骨肉相连的牵挂。我和父亲在相隔四个月又六天之后第一次相见，却是我在法庭上见证那个叫做游 精佑的在接受莫须有的罪名带给他的审判，我的父亲也是见证者。所有了解事情始末得人都会相信父亲不做不义之事，更别说诬告陷害这样的卑劣名号。我们一家人 之前就因为工作和学习原因聚少离多，而今却遭遇如此的打击，这四个多月来我们家人的身心都遭遇着极大地煎熬与折磨。每天我都因为思念父亲而夜不能寐，不单 单是因为我的气愤，更是因为我因此事产生的对社会对法治的无奈和蔑视。在起诉书中提到游精佑等人“行为卑鄙，手段恶劣”，更让我感到愤怒与崩溃。警方可以 用事实指控爸爸，但是不要用卑鄙这一类词语侮辱他！在我的理解中法律文书并不是愤青写帖子，这类情绪化的词应该是很罕见的。如果一个公民因为良知和正义却 得到侮辱和严办，那个这个世界上将没有人敢办好事，如此人情冷漠的世界如何顺应时代发展的脚步？我感到万分沮丧。<br />
<br />
在起诉书 中有很重要的一段写着“林秀英“串通云云，那么主责任者应该是指向林秀英，种种疑点，包括在庭审当日林秀英在法院门外坚守一天，苦苦哀求出庭作证，以及律 师多次要求林秀英出庭作证，被法官拖延直至拒绝，这样不公正的程序实在很难让我们甚至全国和一些外国关注此案的叔叔阿姨们信服。<br />
<br />
在此，我恳求孙阿姨您能依据事实和法律，以仁慈之心，以对一个无助的小女孩的同情之心，还我们一个公道。如果有足够的证据证明父亲有罪，那么我甘愿他受到应得的惩罚！<br />
<br />
高三学生，<br />
<br />
游豫Z</div>

							
							
							
							
