																											<div id="postmessage_607308" class="t_msgfont">2010年5月8日 作者 徐菁 <br />
<br />
拖着疲倦的双腿，按响门铃，如释重负般的一声呼吸后是母亲熟睡后为我开门的那声“你回来了？” <br />
<br />
 <br />
<br />
是的，我回来了，现在已是深夜的23：51分，原本现在的我应该正在洗澡、写日记，正在为第二天的世博园区的志愿工作做准备，然而，现在的我深知，不为我触目的触怒的心绪不做点什么，今晚我一定无眠。 <br />
<br />
 <br />
<br />
 <br />
<br />
<br />
 <br />
<br />
 <br />
<br />
 <br />
<br />
网上看到将近5分钟关于香港TVB的电视新闻视频片段，报道的内容有： <br />
<br />
 <br />
<br />
1.&nbsp; &nbsp;&nbsp; &nbsp; 世博义工（即世博志愿者）阻止香港亚视的记者拍摄中国馆前拥堵的人群，（也许是无心所为）而把记者的摄影机碰坏了。 <br />
<br />
 <br />
<br />
第一，&nbsp; &nbsp;我想问，香港记者们，为什么你们只公布了结果，却不公开原因，无因怎么可能有果？ <br />
<br />
第二，&nbsp; &nbsp;你们亲口说“义工与亚视记者发生拉扯”，可是在00：19分，分明是身着黄色衣袖的女人先推拉世博义工的手臂，那究竟是谁先动手的呢？ <br />
<br />
第三，&nbsp; &nbsp;在三十几秒中，多是香港记者们的唇枪舌战，义工多是沉默。最后有了“火爆局面”，我承认这位义工举动的确冲动，但假设你们香港记者中的任何一个人站在这风口浪尖上，被N张嘴巴冒出的高分贝激动万分的声音拉扯着神经的时候，你们谁还能按耐得住？又何况是一个涉世不深的学生。 <br />
<br />
 <br />
<br />
 <br />
<br />
2.&nbsp; &nbsp;&nbsp; &nbsp; 世博会秩序混乱 <br />
<br />
 <br />
<br />
世博会办在中国，这是第一次在发展中国家举办，第一次在人口如此密集的国家举办，这原本就注定是一件特殊的大难事。中国人口原本就占了全世界人口的五分之一，人多之处，必然牵涉到秩序问题，问题不是不存在，关键是如何去解决。 <br />
<br />
 <br />
<br />
我敢问，香港记者们，新闻工作原本就不应以影响到世博正常工作的前提下进行，你们在人群中凑热闹，不碰坏摄影机还能怪谁呢？ <br />
<br />
 <br />
<br />
 <br />
<br />
3.&nbsp; &nbsp;&nbsp; &nbsp; 世博会秩序混乱成为“国际媒体采访重点” <br />
<br />
 <br />
<br />
上海人不是不懂英语，01：30分，那个外国主持人明明表示，排队需要4个小时，这在历年世博会也是常见现象。 <br />
<br />
 <br />
<br />
 <br />
<br />
4“享世博还不一定有机会，现在上海人在自家门口就可以饱览外国文化，上海人说什么都要来世博会朝圣。” <br />
<br />
 <br />
<br />
第一，&nbsp; &nbsp;对不起，上海人不是去不起国外享世博，我身边就已有许多朋友和同学已经身在国外，他们或求学或工作或被国外邀请学术交流，其次，上海每年也有无数国际交流与交往，我们同样“大可饱览外国文化”，世博会实在不算什么。 <br />
<br />
 <br />
<br />
第二，&nbsp; &nbsp;香港记者们，你们难道不知道中央政府一贯强调“上海是中国的上海”吗？世博会的游客难道仅仅是上海人吗？世博会值得上升到“朝圣”的理性高度吗？――如果您不认为这是在嘲讽的话，我大可作为无知来理解，除此之外，我没有第三种揣测，也请原谅我的学疏寡闻。 <br />
<br />
 <br />
<br />
 <br />
<br />
5.“上海人的素养有没有跟上脚步？” <br />
<br />
 <br />
<br />
02：10分，主持人质问“上海人的素养有没有跟上脚步”？然后播放了几段缺少公德心的行为。 <br />
<br />
 <br />
<br />
第一，&nbsp; &nbsp;我想问，您是怎么确定那些在芬兰馆前“捡起漂亮装饰”的、“踩踏草地导致寸草不生”的、在日本馆前“拿了一大叠免费团扇”的人是“上海人”呢？！如果没有确切的数据，请不要利用大众传媒来误导群众，谢谢您的配合。 <br />
<br />
 <br />
<br />
第二，&nbsp; &nbsp;利用“打开的背包”在世博园区内做一个公德心的测验，我十分欣赏这个创意。这让我想起早先公开的视频，比如恶徒暴打无辜者，围观者竟无一人主持正义。中国人变得很冷漠。可是什么使得中国人变得冷漠？这是我一直思考的问题。这是题外话，我建议香港记者们，不仅在上海做一下这个实验，在香港也同样做一下。如果结果迥异，我只能说你们幸好没经历文化大革命。很多问题，不能只看眼前，而是要从完整的视角去分析的，请你们也自觉维护中华民族的形象。 <br />
<br />
 <br />
<br />
 <br />
<br />
6.“上海民众还有六个月的大考验。” <br />
<br />
 <br />
<br />
04：12分，垃圾乱扔问题，直指“上海民众还有六个月的大考验。”我想说的是，上海是中国的上海，也就是说，您凭什么说那些恶俗恶习就是上海人的行为？是不是以偏概全了？您是不是应该对这座城市有所了解后再对上海人作出评论？这才更符合真理，也更对您的工作负责。当然了，不负责的香港记者长年累月地报道一些八卦小道消息，这些，我也同样“习惯”了。 <br />
<br />
&nbsp;&nbsp;<br />
<br />
 <br />
<br />
 <br />
<br />
我已经公开我的身份了，是因为我对自己的言论深信不疑并且秉着客观负责的态度，如有异议，我十分欢迎您当面和我交流。（电邮地址：<a href="mailto:dcfxu@sina.cn">dcfxu@sina.cn</a>） <br />
<br />
 <br />
<br />
 <br />
<br />
 <br />
<br />
我不否认我是世博志愿者，我也不否认我是一个上海人，全然能在深夜长篇累牍地写下这篇文章，是希望有关媒体能够客观地报道属于“中华民族”的事情，这不关乎一种赌气，也不关乎一种看热闹的心情。无论世博会发生在上海，或是香港，也必然会有许多问题出现。“理解、沟通、交流”这也是世博会的意义所在。 <br />
<br />
 <br />
<br />
 <br />
<br />
 <br />
<br />
有人说我们傻，因为世博会志愿者是一份没有酬劳却又倍苦不堪的傻事；有人说我们是为了赚学分或者学校逼着来的，很抱歉，这或许是少部分学校的事情，绝大多数志愿者只是冲着很单纯的目的而来的，有人调侃世博会是SB会,有人用小镜头定格丑陋现象不发扬积极面……每当诸如此类有关攻击世博会的负面力量袭来时，我想到的不是我在这件事上付出了多少时间、精力和爱心，而总是会有这样的一幅画面在我脑海中挥之不去： <br />
<br />
 <br />
<br />
 <br />
<br />
我的妈妈、爸爸、奶奶，我的家人，第一眼看着我身着2010年上海世博会绿色制服时那份难以言表的快乐，那份快乐里也许带着一种自豪、也许带着一份期望、也许带着一份羡慕……个中杂味，外人所不能道也。 <br />
<br />
 <br />
<br />
 <br />
<br />
妈妈说，“这是中国人第一次办的世博会，也许百年里再也不会有第二次了。百年前没有，百年后也可能不会有了。” <br />
<br />
 <br />
<br />
 <br />
<br />
霎那间，我感觉这一刻是要被载入史册的，而在大背景下的小人物故事，才变得格外有意义，于是我开始意识到我要为上海的形象添砖加瓦，竭力竭诚做好这份志愿工作上的每一件小事，因为每一件小事都会影响中外游客对这座城市的印象，都会增加上海人对世博会的好感。 <br />
<br />
 <br />
<br />
 <br />
<br />
我很感谢，自己所见之处都是那些懂得感恩的可爱的人、那些疑念化解时的豁然微笑，没有抱怨的游客，那一声声的谢谢，那一次次的帮助，也同样帮助我懂得“每一个人都值得尊重，每一种生命都值得去理解。”这也将有助于我今后的媒体工作。 <br />
<br />
 <br />
<br />
 <br />
<br />
我很感谢，我的家人，从没教会我要计较眼前得失，因为有失必有得，他们都是公益事业的热爱者，他们同样也是上海这座让文艺作品渲染得过多纸醉金迷的城市里最朴实无华而又默默献身努力了一辈子的老百姓。 <br />
<br />
 <br />
<br />
 <br />
<br />
而你呢？我可爱的生活在上海的兄弟姐妹乡亲父老们，我可爱的一脉相承“中华民族”血统的兄弟姐妹乡亲父老们，你们又准备在这一场历史事件中扮演怎样的一种角色？</div>

							
							
							
							
