																											<div id="postmessage_779928" class="t_msgfont">作者：推倒柏林墙<br />
<br />
很多中国人羡慕美国佬可以自由的使用枪械，幻想着哪天也能雄赳赳气昂昂一 枪打死开发商。我觉得这实在是妄自菲薄，在我们中国，家里不存着点液化气罐燃烧瓶什么的，你都不好意思跟人说自己是钉子户。如今广西人枪杀公务员，湖南人 炸掉税务局，武汉人炮轰拆迁队。祖国如此自由，你还觉着外国的月亮就比中国圆，这不是赤裸裸的崇洋媚外吗？ <br />
<br />
<br />
暴 力手段的升级还只是一方面，更有意思的是民意的变迁。前段时间流行的口号“冤有头债有主出门右拐是政府”清楚明白的表明了部分民众对官员的死亡表示完全无 所谓，给一小水果摊开出三千多万元税单的长沙税务局被炸飞之后腾讯网上对此事感到高兴的网友更是多达24万，表示难过的则只有六千，吓得腾讯赶紧就把民意 给屏蔽了。一个国家能发展到官员一死人们就比过年还高兴的地步，我觉得这也算是变态到一定的境界了。<br />
<br />
俗话 说“世上没有无缘无故的恨”，对这种现状我们的政府只能说是咎由自取。前段时间茅于轼说了一段话：“收入差距和贪污腐化是社会矛盾的重要方面，但不是民怨 的根源，把根源看错了治理就不能见效。我认为问题的根子在政府不讲理。”这话真是一语中的。我们这个政府一向是你跟他讲道理他拿你擦鼻涕，谁老实就冲着谁 翻白眼，甚至于唐福珍以自焚对抗拆迁之后，有关部门都没有流露出一丝怜悯之情，而是定性为“暴力抗法”。这就教会了人们：跟中国政府打交道，说理是没有用 的，下跪磕头也换不到同情，就连自残都纯粹是浪费自己的生命。我记得几年前每逢有农民工爬吊塔跳楼讨薪之类的事情，就有人摆出一副高学历人士的架势点评说 “农民工缺乏法律意识，不懂怎么用法律武器为自己讨回公道”，说得好像中国的法院真的是用来讨公道的一样。在我党已经成功把访民集体变成精神病的今天，这 类装逼犯总算是基本死绝了，可见人民群众的思想觉悟还是在不断提高的。<br />
<br />
中国政府为何“不讲理”？我认为最 大的原因在于两个字：恐惧。有人大概会奇怪了，我党一手握枪杆子一手握笔杆子，而那些赤手空拳的反党人士则只能成天在活跃中文用户可能不超过十万人的推特 上磨磨嘴皮子，一回墙内就被无数爱国粪青围剿，我党又有何惧哉？其实不然，就连老毛这样呼风唤雨的人晚年时都整天搞得神经兮兮的，无时无刻不在担心别人要 谋害自己，一个人越是了解权力的牛逼之处、越是靠权力去制造恐惧，也就越是害怕失去权力的后果。偏偏我党在当今世界诸多的独裁政党里也算是极为耀眼的脑残 之星，创造了文化大革命这等人类历史上独一无二的害人害己的伟业。安全感的极度匮乏使得他们产生了强烈的受迫害妄想症，惯于用敌我思维来看待问题，甚至把 善意的批评都当成是居心叵测的阴谋，无怪乎六一四学生单单是下跪请愿，李鹏都能认定这是“文革造反派惯用的手法”。前段时间南都便披露长沙开福区一副区长 在工作会议上要求公安对进京上访人员要“按敌对势力办”，苏州通安事件中政府最后也将民变定性为“境外势力操控”，既然他们都拿老百姓当阶级敌人对付了， 那还跟你讲什么道理？<br />
<br />
中国政府每一次的“不讲理”，都会使群众的“忍耐底线”进一步降低，而群众的退让则助长了政府进一步掠夺民财、侵犯民权的胆量。六一四一屠一杀 是毛时代之后我党干的最不讲理的一件事，自此之后社会便全面溃败，当年的大学生还敢站出来反对太子党“官倒”，如今官倒已经泛滥到全党参与，当代的大学生 们却连这个词是什么意思都不知道；当年合肥大学生敢于写大字报抨击人大代表选举不公，上海大学生可以站出来争取新闻自由，你再看看当今的社会，当今的学 生，便可知道自89年之后，无论是政府还是人民，中国都一直处于急速倒退的状态。有人说了，不对呀，经济还是在进步的！这纯粹是胡话，经济发展是市场的功 劳，不是共产党的功劳，共产党的所为只是越来越贪婪的利用自己的特权在市场里捣乱。至于称赞共产党恢复了市场，就跟WOW玩家感激版署迷途知返浪子回头重 新通过了WLK的审批一样可笑，正应了那个苏联老笑话：社会主义最大的优越性，在于能够战胜别的主义下根本就不存在的困难。<br />
<br />
我 党一看到别人对他们说三道四就觉得浑身不爽，从李鹏日记来看89年时除了赵紫阳等极少数开明人士外，高层官员根本就没有去考虑过对话和官倒的问题，他们唯 一所在意的就是如何去平息运动，也就是今天所说的“维稳”。老百姓想要的“稳定”是不受别人的欺压，我党想要的“稳定”却是让人们敢怒而不敢言，“不敢 言”的状态又会反过来增长欺压者的气焰，结果只能是“越维稳越不稳”。从表面上看，六一四 之后中国社会似乎是万马齐喑，人民群众已经集体臣服于机枪坦克之下，实际上中国人的“忍耐底线”虽然降低了，却不可能彻底消失，总有一天又会容不下政府无 限膨胀的胃口。当和平的抗争一次次遭遇挫折时，群众的手段便会越来越激进，这反过来又刺激到政府脆弱的小心灵，使得他们也越来越不讲理，双方的互动陷入恶 性循环之中，最终导致社会逐渐割裂，形成无法消解的敌意。<br />
<br />
我在08年时说过“暴力化”将是中国社会未来的必然走势，事实上今天的社会早就已经完成了意识形态上的暴力化，处处充斥着教科书中“落后就要挨打”和历史观上“成王败寇”之类的武力崇拜，所欠缺的已经仅仅是实践出真知了。爱国粪青在谈及六一四 学生时都能潇洒的说出“杀得好”这三个字，俗话说天下党员出粪青，而且还是逆向淘汰机制下过五关斩六将之后脱颖而出的傻逼中的战斗机，这帮人身为屁民的时 候都能站在统治者的角度的思考，等他们真的摇身一变成为统治者时，我相信他们更可以心安理得毫不犹豫的镇压甚至屠杀一切反对他们的老百姓了。如今暴力既是 我党解决问题的终极手段，也是人民寻求正义的唯一工具。而且你千万不要以为这只是一小撮走投无路的屁民和共产党之间的私怨，和你我这样的普通人就毫无关系 了。不是每一个人都能像杨佳同志一样把斗争的矛头指准方向，一个人在恐惧之中压抑得越久，爆发的时候就越愤怒，而恐惧和愤怒都是能够摧毁理性的情感。我党 官员躲在用纳税人那里收来的保护费所构筑的坚硬龟壳里，弱小的屁民们无计可施，其愤怒无处发泄，那怎么办？最简单的方法当然是拿其它屁民开刀了。<br />
<br />
最 近我国已经进入了屠童事件高发期，据报道南平血案之后短短一个半月的时间，单是北京警方就已经制止了七起针对幼儿园的袭击，抓捕了十名犯罪嫌疑人。我看到 这新闻时真是不寒而栗，也不知道这帮警察是在路上随便抓了几个人就带回去邀功领赏，还是杀小朋友真的已经成了时尚潮流，总之都不是什么好事。前段时间爆破 了长沙税务局的哥们同样表示下一步就要对付高官子女，陕西镇南屠童案之后某受害儿童家长更是因不满政府的赔偿方案，提起刀来就要去杀别人家的小孩。不光是 幼儿园里血案多，两个月前一被截访女子在火车上趁人熟睡之机连捅九个素不相识的乘客，你说这火车坐得好好的，谁他妈能料到旁边就有这么一号人物？我党虽然 有城管武警解放军等一系列大规模杀伤性武器，对这类“敌在暗我在明”的偷袭却是无计可施。像爆破之类的已经算是高端手法了，在中国这种奶粉里掺了三聚氰胺 都查不出来的国家用纵火投毒这样低门槛低风险高收益的手段来报复社会那更是防不慎防。要知道牛逼如美国还不是照样给人撞掉了双子大楼，弱势如维族也可以到 处拿着针头注射不明液体，人家正面战场打不过你，背地里往你家门锁里灌点胶水还不行吗？<br />
<br />
这草根暴力的指向 目标还具备极高的不确定性，除非是已经彻底脱离了群众队伍、出行都不屑于买头等舱而是坐私人飞机的超成功人士，否则任何人都有可能是潜在的受害者。如今多 数人可能还觉着这类倒霉事就像强制拆迁一样离自己十分之遥远，其实中国现在还不过是处于暴力社会初级阶段。我在《是谁在分裂中国》里说过，民族仇恨是一个 矛盾放大器，少数民族的今天其实就是我们的明天。前年有西藏三一四，去年有新疆七五，按照这个势头发展下去，以后内地就不是少数民族杀汉族人，而是汉族人 以同样的力度自相残杀了。很快人们就会因置身于暴民与暴政的交叉火力之间而人人自危，而且被暴民伤害的可能性更高、后果更惨重。<br />
<br />
从 客观上来讲，滥伤无辜的恐怖主义行径对中国政府的打击恰恰是最为致命的。共产党何以长期维持自己的暴政？答案同样是两个字：恐惧。在中国多数人都曾经历过 如下的转变过程：虽然他们的内心对现状并不满意，但是他们清醒的意识到改变社会需要付出极为高昂的代价，甚至有可能是自己的生命，因此他们的主张逐渐由 “社会需要改变”变成了“社会无法改变”，麻木的安于现状；而他们却又不愿承认并正视自己的懦弱，为了合理化自己的不作为，慢慢又开始改口宣称“社会无需 改变”；有些人甚至堕落为彻底的犬儒主义者，疯狂的攻击一切试图改变社会的人。今天中国人实际上是在遵循一套潜规则：只要服从党的统治，至少可以保证性命 无忧。不时放出一点经济上升、奥运世博之类的“利好消息”，就可以让他们安慰自己“潜规则依然运转良好”。如今的民运势力为何不成气候，关键就在于缺乏民 意的支持。想当年波兰一弹丸小国随便搞一次运动都是十万甚至百万人级别的规模，而在当今国人的眼中，海外民运不是轮子教的信徒就是89年投机失败的西方操 控的走狗，国内民运更是生活郁郁不得志试图对共产党取而代之的社会渣滓，不愿遵守潜规则的人在人们看来比共产党还要更为可恶。前段时间喻高之所以猛批艾未 未，其原因正在于此。如果说一般的反党人士只是对潜规则口诛笔伐，暴民们的行为则是对潜规则最直接的破坏，一旦人们意识到即使服从也不能保障安全，对执政 党的信心便会急速下降，我党的统治将以惊人的速度土崩瓦解，无序的暴力将转化为有序的暴力。换一个如今已经不太常用的词来说，“革命”就是这样点燃的。<br />
<br />
有 人恐怕要说了：你这转换未免也太搞笑了，今天的中国横看竖看也不可能会发生革命啊？我觉得这没什么不可能的，历史上所谓的革命从来就不是抛出一个什么先进 思想来群众就会跟着你混的。当年孙中山初出道的时候，在世人眼里不过是个“高谈时政，放言无忌”的二逼愣头青，在国内的评价可能不比今天的轮子教好得到哪 里去，但是八国联军入侵之后，立刻“时势异也”，“清廷威望扫地，民心思变”，孙中山摇身一变成为进步领袖，革命党势力急剧扩张。如果不是因为日本人使国 民党丧失了对半壁江山的控制力，共产党单凭苏联的支援同样无法成事，绝大多数成功的革命背后往往都有这么一个导致“民心思变”或是“政权崩溃”的契机。如 今虽然外族侵略的可能性基本为零，但我并不担心这个契机不出现，反而担心它会以怎样的形式出现。三民主义里最有价值的部分是“民权”，多数革命党人对其却 毫无兴趣，反倒是“民族”更能笼络人心，正是因为八国联军的入侵使得当时中国社会只承受了极小的代价就完成了民意的转变。今天的中国失去了民族仇恨这个老 牌强效放大器，所要付出的代价必然比清朝人民要高昂得多，从这个角度来看没有外族的威胁反而是件坏事。89年时柴玲说中国政府“无赖之极”，说“我们就是 要在这里流血，用我们的鲜血和生命来唤起民众”。前一句话说对了，中国政府确实无赖之极；后一句则是天真了：几个大学生的鲜血根本就唤不醒中国的民众，要 想让人们搞清状况非得流他们自己的血不可。当年柴玲没好意思公开号召她的战友们去流血，我觉得这根本不用去号召，中国总有一天要血流成河，不管你愿意不愿 意都是这个结果。以前饱醉豚写了篇文章叫《你愿意为自由民主付出什么代价》，回帖者中基本上没有几个人是敢正面宣称自己要送胳膊送腿的。很快大家就不用再 纠结这个问题了，因为人们将亲眼目睹忍受专制独裁又要付出什么样的代价。<br />
<br />
美国人常说“Freedom is not free”，长期以来民运人士却总想寻求不流血的中间道路。事实上以前我也认同冉匪“日拱一卒，不期速成”的观点，希望靠开启民智来获得廉价民主，后来才 发现中国人所缺的不仅是智力，更是敢于正视现实的胆量，而一个装睡的人你是永远也叫不醒的。今天的中国实际上正在进行一场民主派与共产党之间的赛跑，看到 底是前者开启民智快还是后者开启民愤快，前者获胜则中国多半能迎来非暴力的和平转型，后者获胜则大家只能坐等天下大乱，玩的就是一个“速成”。很不幸，以 这几年的情况来看，恐怕多数人都会认同民主派已经败得毫无悬念，毫无翻盘的余地。当然这也不是说开启民智就没有意义了，一来民愤并不等于民智，搞不好会比 天京事变还要恐怖，民主派们也只能替共产党擦擦屁股，尽量减少不必要的损失；二来今天的民运人士里也不乏一些神经病，民主英雄郭泉同志曾一度声称自己组建 的新民党有七千万党员，和共产党齐头并进，比轮子教三退还来劲，其中跟他有直接联系的访民更是多达一千万人，郭教授卖汉服甩棍还来不及，何时有这等超人的 精力？当时我有一朋友给郭泉打电话，闲聊一番后问他新民党从哪里搞来那么多人，孰料郭泉怒不可遏，厉声质问我的朋友是不是共产党的特务。我在推特上说了这 件事之后，很快也被扣了个共党特务的帽子，我隐藏得那么深都能被他识破，你看看这智商高到了何种程度。当年南京某大学副教授也把自己当成了世间唯一合格的 民主发言人，有一天突然给我私信，问我是不是卧底的五毛。我当时懒得搭理他，回他“这都被你发现了”，不料这厮的情商比Sheldon还低，居然得意洋洋 的说：我早就知道了，你不仅是五毛，而且还是五毛中的高级领导。这蠢货还曾经见过我一面，我要是二十岁就能当上五毛小队长，那三十岁时岂不是应该候补政治 局委员？以后要是让这帮小毛泽东成了事，我看笑蜀又得编一本《历史的先声第二弹》了。如今民主派的任务已经不是推翻现在这个共产党，而是扼杀掉下一个共产 党。现在这个其实不需要操心太多，他们自尽的速度远超你的想象，只不过临死前还要拉上全国人民陪葬罢了。<br />
<br />
要 想避免“暴力化”造成的可怕后果，唯有进行彻底的政治改革以改善社会公正，而要我国政府做到这一点却又几乎等于是天方夜谭。以我长期观察的经验来看，受恐 惧所控制的人一般都有两个特点，一是喜欢靠歪曲现实来安慰自己“情况还没有那么糟糕”，二是固守陈规，害怕变化所带来的不确定性。要是连我党官员的水平都 能意识到问题已经严重到了非改革不可的地步，事情恐怕早就已经无法挽回了。清政府直到武昌起义之后才匆匆推出《宪法重大信条十九条》，一一满足革命党人的 要求，那时候革命党已经稳占上风，你再去妥协还有个蛋用？事实上终身制世袭制的统治集团还比共产党更加珍惜自己的江山，如今我国流行裸官，一见大事不妙连 金银细软都不用收拾就可以拔腿跑路。众官员们恐怕都想着只要自己任上不要出事就OK，哪管身后洪水滔天，玩起了击鼓传花的游戏，何况职位越高的官员受社会 暴力影响的程度就越小，横看竖看他们都是最无所谓的人。也不用指望党内会出现什么信奉民主自由的先进人士出来力挽狂澜，即使是胡锦涛突然宣布自己其实是长 期潜伏于共产党内的戈尔巴乔夫，我看他也只能和赵紫阳携手相伴黄泉路。改革派在党内得势的基础是社会上强势的党外反对派，而我前文说过如今的民运势力根本 不成气候，如果他们哪天真的上了台面，那就说明暴力社会高级阶段已经到来，共产党大概也没几天好日子可过，该流的血也差不多流完了。<br />
<br />
其 实在中国这潭死水里呆的时间长了，连我自己也不太相信事情真就会朝那么夸张的方向发展，但从纯逻辑角度来看似乎又只能是这样，近年来这个社会所发生的一些 事件更为我的担忧提供了越来越多的论据。社会的巨变从来就是难于预测的，封从德的日记显示此君在1989年4月15号那天同样浑浑噩噩的活在一潭死水中， 就算你告诉他接下来两个月会发生的事情，又要让他如何相信？如果说历史有它的偶然性和必然性，那么六一四 运动就是社会条件所形成的必然，胡耀邦的死则是偶然的导火索，但是即使他没挑那个点去世，炸弹也总归会以别的形式引爆，而人们之所以不相信“必然”，是因 为几乎没有人有能力去看清那些“偶然”。我本人当然也希望自己只是大嘴巴抡一抡，跟贝利一样预测全部不中，不过思前想后，我还是决定三十六计走为上。套一 句三俗派代表马诺的名言，那就是：宁愿在国外检讨自己的弱智，也不要在雨花功德园三万元一平米的墓地里自我欣赏。</div>

							
							
							
							
