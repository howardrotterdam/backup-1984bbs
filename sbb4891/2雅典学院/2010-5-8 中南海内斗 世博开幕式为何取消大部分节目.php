													<h2>中南海内斗 世博开幕式为何取消大部分节目</h2>
						
						
																											<div id="postmessage_603813" class="t_msgfont">中南海内斗 世博开幕式为何取消大部分节目 <br />
<br />
――没有一代“盛世”，如此恐惧地呈现<br />
<br />
上海世博会开幕，虚荣心十足的中国政府，照例摆阔耍富，大操大办。据称，当晚所放焰火，比北京奥运会还多了2万多发。而京奥焰火，就比世界历届奥运所放焰火的总数还要多。但上海世博会开幕仪式，却出人意料的简单，演出只一个小时，便匆匆结束。<br />
<br />
<br />
<br />
<br />
知情者透露：因抄袭和剽窃屡屡被外国媒体揪住（上海世博会主题曲、吉祥物、“中国馆”造型等，均涉嫌抄袭），世博会内部，自我检查，又发现更多抄袭和剽窃，当局害怕爆出更多丑闻、闹出更多洋相，干脆取消了相当部分已经排练好的节目。<br />
<br />
两年前的北京奥运，中南海施出浑身解数，不惜代价，邀请了80国首脑出席，以展现“万邦来仪”的“天朝”气派。上海世博，中南海图谋如法炮制，公开声称，要展开“世博外交”。台前幕后活动多年，砸下无数银子，无奈这份“外交成绩单”，却并不理想，到开幕时，才勉强凑齐20个国家和组织的首脑。<br />
<br />
展开名单，西方发达国家中，只有法国总统、荷兰首相和欧盟主席；亚洲发达国家中，只有韩国总统；其他国家政要，则来自诸如北朝鲜、柬埔寨、刚果、加蓬、巴勒斯坦等第三世界。<br />
<br />
大多数外国首脑婉拒中共邀请，缺席上海世博，部分原因，源自北京奥运时，他们上当受骗的教训。那时，中南海假装摆出一付要“改变”的样子：开放外国媒体和记者到中国采访；也临时开放了互联网；甚至在国际压力下，假意与达赖喇嘛的代表谈判。一些外国政要以为，二十年前“奥运会带给韩国民主化”的好事可能在中国重演；中国的人权问题、西藏问题等，可能在一定程度上获得良性解决。<br />
<br />
岂料，北京奥运一结束，中共便打翻台子，翻脸不认人。在国内，封锁互联网，日盛一日；践踏人权，变本加厉；干下迫害高智晟、重判刘晓波、主导新疆屠杀等累累重案。在国际上，叫板文明世界，展示强硬嘴脸；甚至悍然中断与达赖喇嘛一方的接触（长达15个月。直到今年初，眼看无法阻挡美国总统会见达赖喇嘛之时，才赶急般地抢先与达赖喇嘛代表“接触”一回）。<br />
<br />
多数国家首脑、尤其多数西方国家政要，拒绝出席上海世博会，验证一句西谚：“你可以骗人一时，不可能骗人永久。”死要面子的中共，这一回，并没有捞到多少面子。<br />
<br />
“世界在你眼前，我们在你身边。”这是悬挂在虹桥机场的一幅世博标语，仿佛对每一个到访者提醒：你已经被监视。为了世博会，当局调动的保安人员，多达86万，人群中，便衣与线民，比比皆是。<br />
<br />
开幕当晚，当局狂放焰火，但涌到黄浦江边的数十万民众，却大呼失望，因为，除了能看到一些灯光，几乎看不见焰火。原来，当局大范围戒严，措施严厉，民众被遥遥隔绝在世博会现场之外，加上高楼大厦的阻挡，焰火什么的，几乎看不到。世博会开幕之日，平日里热闹非凡的浦东，突然变得一片死寂，除了三步一岗、五步一哨的警察，行人和车辆，一律“被消失”。<br />
<br />
纵观历史，没有一代“盛世”，是在如此恐惧的气氛中呈现。正如北京奥运会一样，上海世博会，不是人民的盛会，也不是国际的盛会，而仅仅是中共官僚集团的盛会，小圈子里的自娱自乐。<br />
<br />
“城市，让生活更美好。”这是上海世博会的主题。但，那个“让”字后面，缺少宾语，究竟让谁的生活更美好？显然不是那些为了世博会而被强拆强迁、补偿不公的胡燕们，也不是那些为了世博会而流尽血汗的外地农民工，更不是流离失所、被打被关的访民们。说穿了，不是让劳苦大众的生活更美好，而是让贪官污吏的生活更美好。好一个反讽的世博主题！<br />
<br />
就在上海世博园区，有一个被国际上称作“邪恶角落”的展区，集中了伊朗、苏丹、北朝鲜等国的展览馆，它们的主题命名，同样具有反讽的“笑果”：以宗教狂热和信仰歧视著称的伊朗，自命其展馆主题为“多元文化交融的城市”；曾屠杀30万达尔富尔人的苏丹军事当局，自命其展馆主题为“城市与和平”；制造当代饥荒之最的北朝鲜金正日政权，自命其展馆主题为“人民的天堂”。<br />
<br />
“胡哥好”，这是世博开幕当夜，在世博轴上打出的灯光标语。立即让人联想到，1984年，中共庆祝建政35周年，游行队伍中，有人打出“小平您好”的标语。据说，那幅标语，由北京大学的几个学生“自发”打出。而5年后，1989年，正是那个曾被学生们颂称“小平您好”的邓小平，调动数十万军队、坦克，对和平请愿的学生和民众大开杀戒，血洗京城。今日，那些高呼“胡哥好”的人，莫非又在讨一个“杀”字？事实上，胡某主导的屠杀，前后已有三轮：1989年，西藏；2008年，西藏；2009年，新疆。<br />
<br />
观今日之势，出现在世博轴上的“胡哥好”标语，绝非普通民众“自发”所为。且不说，奴性到这等程度，毕竟少见；就说，当局严格的保安措施，也不可能给任何民众任何作为的空间。推论是，这条标语，只能来自中共政权内部。要么是上海当地官员所为，表现对胡锦涛的阿谀奉承；要么是胡当局指示，自我宣传。<br />
<br />
实际上，高高挂出的这条标语，放射的真正含义，乃是胡江权力斗争、胡胜江败的当前结果。上海，是江泽民的权力基地，但上海世博会开幕，好出风头的江，居然没有露脸，跌破世人眼镜。可怜见，任期将尽，“胡锦涛时代”才终于来临？<br />
<br />
联想到不久前，现任总理温家宝公开发文缅怀胡耀邦（改革派代表人物），而几乎同时，前副总理曾培炎则公开发文歌颂江泽民（保守派代表人物）。一出对台戏，一直上演到世博会。江泽民首度缺席重大场合，不是权争失势，就是赌气不出；温家宝再度前往青海地震灾区，与其说是“心系灾民”，不如说是赌气不来。愈到任期将尽，温某愈是无所顾忌，似乎公开宣示：我跟他们不一样。他们迷恋荣华富贵，我自牵挂民生民艰。<br />
<br />
权力较劲，也泄露另一个不是秘密的“秘密”：当今中南海，已经四分五裂；各有其主，各有其从；谁也不服谁，谁也不怕谁。大戏如何收场？世人还可拭目以待。<br />
