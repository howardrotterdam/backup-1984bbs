<br />
　　周恩来对于江青，用他自己的话叫做：&quot;我已经仁至义尽。&quot; 江青当年刚进入毛泽东的窑洞，引起中共高层各方非议，认为毛以党的领袖之尊，将经历长征共同患难的妻子贺子珍赶走，炕头尚温，就公开与上海影星同居，成何体统？只有极少数人支持毛、江的结合，周恩来便是其中之一。另外还有康生，他提供来自特工系统的调查证明，江青在白区&quot;表现清白&quot;。他是山东诸城人，江青的老乡，传说在家乡做少爷时代就和她&quot;有一腿&quot;，查无实证。他的支持除了老乡情谊，主要是投毛所好，有向新主子表现忠诚的意思。另一个是贺龙。他扬言&quot;堂堂主席，搞个把女人算什么！&quot;但真正有力支持者还是周恩来，正是他调和各方意见，力排众议，铺设堂火，毕竟成了他俩的好事。否则，便不会有日后权倾朝野的江青同志了。周恩来的出发点是否比康生要高尚一些？看不出来。 <br />
<br />
　　在多年的媳妇熬成婆之前，江青与毛泽东经常发生争吵，斗气。无非生活习惯的差异，鸡毛蒜皮本是夫妻间难免的事。但双方的个性都极强，闹到各不相让，江青便只得找周恩来哭诉，由他出面调解、劝说，让毛泽东化怒气为春风，让江青破涕为笑。正如当初异口同声地反对，中共中央机关从最高领导层到一般警卫、秘书，没有几个从心理承认和瞧得起这位夫人，只有周恩来对她待之以礼，不含半点歧视和轻蔑。这给心性敏感好强的江青以极大的精神慰籍，使她逐渐化解敌意环境带来的伤痛，重新建立自己的形像和自尊、自信，终于等到二十年&quot;约法&quot;到期，一跃而登上中国的政治舞台。一个人做一点好事并不难，难的是一辈子做好事。周恩来在毛、江家庭关系上做好事，充当黏合剂与稳定剂的作用，绝不仅仅在延安时代，而是持续到建政以后，甚至文革期间。都说周恩来是毛泽东的&quot;大管家&quot;，管家管家，毛的家他不管谁管？<br />
<br />
力助江青坐稳第一夫人交椅 <br />
<br />
　　一九四五年八月，毛泽东赴重庆谈判，一首谈笑古今、暗含霸王之风的《沁园春．雪》，争取了天真的第三党和文化界的众多知名人物，以为寄托中国未来的希望所在，因而在其后三年的国共争霸战争中，感情倾向多在共产党一边，形成国民党大势已去、无力回天的局面。毛泽东此行，周恩来功不可没就不须细说了，单说他派专机从延安接江青来重庆&quot;治疗牙病&quot;一节，就可知他的煞费苦心。这是江青首次以中共第一夫人的身份公开亮相，为了是这一身份合法化，周恩来在张治中的寓所为毛、江二人补办了盛大婚礼，江青后来经历了贺子珍回国、癌症、更年期、老年色衰等动摇&quot;夫人&quot;地位的重大危机；而每一次毛都终于放弃&quot;废后&quot;的念头，与周恩来当年安排的这一婚礼既成的影响不无关系。 <br />
<br />
　　一九四七年，贺子珍带女儿娇娇随王稼祥夫妇从苏联回国，暂居哈尔滨。江青大为紧张。因为他曾接受的&quot;约法三章&quot;的第一章就是，&quot;毛泽东、贺子珍的夫妻关系尚在，而没有正式解除时，江青不能以毛泽东夫人自居。&quot;毛、贺的&quot;夫妻关系&quot;，一直到死都&quot;没有正式解除&quot;过。一九四八年冬，贺子珍的妹妹、毛泽东弟弟（毛泽覃）的遗孀贺怡找到毛泽东，闹着要为姐姐讨一个公道。毛让步，同意贺子珍回身边，说&quot;还是按中国老传统办吧！&quot;江青深感会要做&quot;小老婆&quot;甚至被休掉的威胁，又跑到周恩来那里一顿哭诉。而周也再一次施以援手，以组织名义作出决定，不准贺子珍进京与毛泽东会面。 <br />
<br />
　　贺怡在哈尔滨接姐姐和侄女娇娇，乘火车走到山海关，被自称&quot;组织部来人&quot;的两个男子以开除党籍相威胁，阻止他们到石家庄。结果，贺子珍被安排到了沈阳，而由贺怡带娇娇去见毛泽东。娇娇后来竟从江青的本姓改名为李敏，更确定江青的继母地位。由于周恩来的悉心部署，贺子珍一直未有机会进京复辟。直到一九五九年庐山会议期间，才由毛泽东另一弟弟（毛泽民）的遗孀的后夫、江西省省长方志纯的秘密安排，将贺从上海接到山上与分别二十年的毛见了一面，也是最后一面。此时的贺子珍，已是一位满头华发、容颜憔悴、语言迟钝的老妪，毫无吸引力可言了。连毛泽东也暗自惊诧：&quot;她怎么变成这般模样？&quot; <br />
江青最信得过的大好人 <br />
<br />
　　这里要补述一段的是，性情刚烈泼辣的贺怡，见到姐姐被弃、大伯子兼姐夫的家庭重组已成定局，心有不甘，于是想出最后一招，到江西寻找毛泽东与贺子珍所生的儿子毛岸红。这是毛的第四子，毛岸红小时活泼可爱，模样酷似毛泽东。一九三四年中共中央红军撤离江西长征逃窜，年馑两岁多的岸红托给了留下打游击的红军独立师师长、毛泽东的弟弟毛泽覃。一次战斗中，岸红被寄放在一个农民家里，后来失去了联系，而毛泽覃也在瑞金红林山区阵亡。江青无子，毛长大成人的两个儿子，毛岸英死于朝鲜战争，毛岸青患有精神病，贺怡深知这个可以找到的儿子对于恢复姐姐的名分的重要性，她又是唯一知道毛岸红线索的人，于是一再深入江西农村寻找。谁知突然的一场车祸，贺怡死于非命。这唯一的线索就此中断。这场车祸完全是意外，还是缘于某种周密的安排？有待新的史料证明。 <br />
<br />
　　周恩来的恩惠，江青当然不会无感于心。从她一进入中共最高层的生活，就把周恩来当作最可以信赖、最善解人意、最能排忧解难的好人。毛泽东说， 江青说：&quot;洪洞县里无好人。其实不对，周恩来就是一个好人。”</div>

							
							
							
							
