																											<div id="postmessage_59973" class="t_msgfont">【刘少奇】<br />
“我完全同意你(江青)的批注和看法，我也是以无比愤怒的心情看着、想着、批注着这三本刘贼叛卖我们党和牺牲同志们的材料。刘贼是大叛徒、大工贼、大内奸、大特务、大汉奸，真是五毒俱全、十恶不赦的反革命分子！我们要首先欢呼我们伟大领袖毛主席亲自发动和领导的这场无产阶级文化大革命。没有这种大革命，怎么能够把刘贼及其一伙人的叛党卖国、杀害同志的罪状，挖得这样深，这样广？当然我们还要继续挖下去，不能有丝毫松懈，不能失掉警惕，如果挖不完，我们要交给后来人！”<br />
<br />
( 周恩来接见首都工宣队军宣队代表时的讲话) <br />
“刘少奇、王光美这些反革命修正主义分子、叛徒、特务，拿清华作试点，压迫清华的革命小将。蒯大富同志，不能因为他犯了这样那样的错误，而轻看了他们的作用，不能否定当时王光美对他们的压力。当时我很清楚嘛！当时他受了很大的压力，禁闭，迫使他们进行绝食。因为当时十一中全会已经开了，正在开着，那时候我奉命去谈话，代表毛主席的声音去解放他们。当时他们是受压迫的一伙人。”<br />
<br />
<br />
【瞿秋白】<br />
（一九六六年八月三十日在接见中国科学院双方代表时的谈话）<br />
“家庭出身好是一好，表现好是两好。家庭出身不好，表现好，亦是一好。出身好表现不好，是忘本；家庭出身不好，表现不好，是两个不好。两个不好，应该回老家去了，或者叫复辟。主要看你的表现，晚节。瞿秋白出身大官僚资产阶级，晚年写了一篇《多余的话》，表现不好，晚年叛变了。我在政治局作了一个报告，还未发表，红卫兵知道了，就去八宝山公墓把碑给砸了。……在知识分子官僚家庭出身的瞿秋白，象李秀成一样，晚年变节了。我们应当向青年历史学家戚本禹同志学习，学习他对李秀成的分析。……瞿秋白被敌人抓住了，卑躬屈膝，最后还是被国民党杀掉了。 ”<br />
<br />
（一九六七年二月一日在接见铁道部部分革命造反派时的讲话）<br />
“我是经过五个时代，从大革命到现在：一九二一～一九二七，一九二七～一九三七，一九三七～一九四五，一九四五～一九四九，一九四九到现在。五个阶段都经历过不少事情，看到过不少阶级，有的是钢铁战士，有的就是不行。象向忠发是党的总书记，但一抓住就叛变。当然也有好的，象苏兆征成为烈士。知识分子也是这样，也有很多烈士，象李大钊；也有象陈独秀背叛革命成为托派，瞿秋白临死前还写叛变书，就象从苏联回来不可一世的王明，也成为修正主义分子，住在苏联，成为人民的敌人。革命烈士很多，革命者是前仆后继。我们是留下来的人，多做一些工作，就是为世界革命服务，你们应该全心全意干革命，要胜过我们。” <br />
<br />
（一九六七年二月八日在接见江苏省革命造反夺权委员会赴京代表时的讲话） <br />
“我建议注意调查研究。我接见过南开大学卫东派组织。我叫他们多作调查，他们下决心钻到很多图书馆里，查了几个月。安子文是黑帮分子，是一位学生调查出来的。南开大学发现了二十多年前的材料。安子文叛变，是经刘少奇批准，集体自首。入城之后，组织部长是安子文，就不把这一部分材料拿出来。瞿秋白死前也写过一篇文章，同李秀成的一样，结果蒋介石还是把他杀了，这篇文章被陆定一藏起来了。这些都是大事，是白纸黑字写的材料。这些材料就值得了。” <br />
<br />
（一九六七年三月二十一日在接见财贸口革命造反派代表时的讲话）<br />
“这次运动搞出了一大批叛徒，这是红卫兵的功勋。瞿秋白死前写了《多余的话》，是叛徒的自白书。我在中央政治局会议上，提出了这个问题，这是从戚本禹同志的文章中得到的启发，年青人启发了我们这些老头子。最近又发现，在枪毙前，瞿秋白写给当局的一封求饶的信。他是个叛徒。这些是刘少奇、邓小平组织路线的错误。安子文掌握了组织部二十多年，他们在表面上比我们还“左”，实际上是反毛泽东思想的。从此，我们对自首的叛徒就严格了。”<br />
<br />
朱德】<br />
“至于朱德同志的账那就更多了。从井冈山一直打到梅县，都是盲动主义，军阀主义，流寇主义。然后是立三路线，你也犯了。然后是王明路线四年，然后又是洛川会议。那时王明没有回来，那还不是反对毛主席，你没有领导？然后王明回来。第二次王明路线一直到六中全会，以后还有一些“残余”。几十年历史，朱德同志跟张国焘斗争，前一半应归功于刘伯承同志的推动。如果没有刘伯承同志在那里，黄袍加身，你顶得住吗？后一半是贺龙同志，弼时同志，关向应同志的共同推动，才北上了。如果没有这些，你甚至滑到河西去了。” <br />
<br />
“解放以后，那多了。毛主席常说，高饶彭黄的事，你都沾过边嘛。你到处发表意见，是一个危险的事。……我们不放心，常委中有这样一个定时炸弹，毛主席也担心。毛主席说过，你就是跑龙套，可是你到处乱说话。你要谈话，得写个稿子，跟我们商量。……所以你是不可靠的，是不能信任的。南昌起义，就是有错误嘛。我当着资产阶级国家的元首尼雷尔的面说：南昌起义，我有错误。他听了很为惊奇：你还有错误？那时错误嘛，城市观点嘛。所以，今天我把我对你（朱德）的不满告诉大家，希望你们大家监督。 ”<br />
<br />
【贺龙】<br />
“贺龙要背靠背的斗，这是最高指示。过几天我和江青同志接见你们。体育界专业队太多，搞锦标主义、“物质刺激”。本来让总政管，现在管不过来。体育界暂时由戚本禹同志管，我和他讲一讲，我一定抽时间。 ”<br />
<br />
【叶剑英】<br />
“叶剑英对罗瑞卿的跳楼作了一首诗：“将军一跳身名裂，故人回首成永诀”。完全站到罗瑞卿一边。 对这个阴谋篡党篡军，篡政的野心家，如此重视，但对我们伟大领袖毛主席没有一点阶级感情。聂、徐、叶在这次会中不如下面的同志好。当主席讲话中，讲到哪个地区的问题时，哪个地区的同志就主动检讨，但讲到国防科委、军事院校……时，刘贤权同志的报告被压以及赵永夫的问题时，他们一点表示也没有，态度很不好。”<br />
<br />
“叶剑英的女儿冲军事科学院（王树声同志为保护档案）大字报也上街了，叶是知道的，他没有制止。 叶剑英的女儿叶向真用绑架的方法，只用了七分钟的时间，就把彭真、罗瑞卿抢走了，引起了以后的全国绑架，此事戚本禹和他们有联系。”<br />
<br />
