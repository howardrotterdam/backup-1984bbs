																											<div id="postmessage_330530" class="t_msgfont">为什么只有进化论，从诞生之日起就遭到一波又一波的质疑？这难道还不能说明问题吗？<br />
=====================<br />
质疑多不能说明什么，只有有效的反驳才是有意义的质疑，质疑数量多只能说明它触犯了某些人的利益，而不能用来证明理论无效。宇宙大爆炸理论一出现就受到教廷的欢迎，不是因为教廷突然成了物理学院所以一改以往对天体物理学的仇视，而是因为宇宙存在起始点为上帝造物提供了可能。单纯的利益而已。<br />
进化论被质疑多，因为它触动了在西方有重要影响力的宗教的基础。<br />
<br />
<br />
以人类为例，虽然其DNA所含核苷酸数量达到30亿，但真正决定细胞复制的部分只占2%，那就是只有6千万，这个数量大致相当于用来分析人类多样性的Y染色体的两倍，应该不算太难对付吧。<br />
================================<br />
劳烦弄清基因的排列特点，不要想当然的以为想测的序列都扎堆站好。自己搜下什么是外显子内含子。<br />
<br />
据报道，科学家们认为人类的基因中能够控制蛋白质复制的部分只占2%！哈哈，刚好是2%。其余的98%都是所谓垃圾基因，功能不明。<br />
====================<br />
只有对分子生物学一窍不通的人才会冒出“垃圾基因”这么个山寨叫法。而且这个报道数据还不对，不是2%，编码区只占人类基因组的1%。别高兴，因为剩下的99%绝对不是你那个什么垃圾基因――基因占一个生物体遗传物质的整套序列的约25%，剩下约75%的序列可以成为垃圾DNA，但绝没有“垃圾基因”这个东西。至于功能，非编码区的功能，内含子已被证明对基因表达有影响，而垃圾DNA的功能也有假说。说了别想当然，基础知识要有，不然跟民科说推翻了相对论一样可笑。<br />
<br />
不知你是否注意到，人类只有23对染色体，<strong>而其他所有灵长类，包括猴子和猩猩，都是24对染色体</strong>。<br />
========================<br />
不知有没有同学注意到，这个说法是完全错误的？<br />
<br />
<br />
关于猪，我有百分之百的把握告诉你，逃到野外的猪，繁殖出来的下一代就是野猪。甚至有传说公猪本身就会长出獠牙。<br />
至于狗，网上有帖子认为狼和狗仍然可以交配。虽然我不确定，但因为狗的各个品种之间都可以杂交，所以这个说法应该可信。<br />
========================<br />
什么是种，什么是亚种，什么是生殖隔离。有兴趣的同学弄清之后就知道他这里犯的是什么错误了。<br />
<br />
<br />
按《出非洲记》的说法，科学家就是因为感到对正常基因的遗传变异感到根本无法跟踪观察，才憋出从线粒体和Y染色体方面下手的点子来的<br />
=======================<br />
什么《出非洲记》我没读过，不过要那书真这么写，说明这是本普及谬误的科普书。用线粒体基因组和Y染色体来研究亲缘性是因为这二者可溯源――线粒体只会来源于母系，而Y染色体只来源于父系，比较起来操作简单。<br />
如果只能弄些不靠谱的科普读物来作为论据的话，还是省省吧。会有人拿《货币战争》这书作为资料来研究经济学么？<br />
<br />
再说一句，现在的进化论不等于达尔文进化论，达尔文当初的理论目前只有1/3得到保留。不过要觉得拿个过时的东西做靶子打的爽，也是你的自由。<br />
<br />
科学不是道德，不以大多数人的认识为基准。过去的常识还太阳围着地球转、物体下落速度跟其质量成正比呢，不都被证明是错误了。</div>

							
							
							
							
