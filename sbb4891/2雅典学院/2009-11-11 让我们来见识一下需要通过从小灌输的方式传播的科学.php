																											<div id="postmessage_323063" class="t_msgfont">献给达尔文的生日礼物<br />
<br />
在小学讲授进化论将是一项抵御无知的‘智慧设计论’的重要举措<br />
<br />
值达尔文诞辰200周年之际，这是献给他的一份伟大的生日礼物。今年晚些时候新版国家小学教程发行时，进化论将破天荒的囊括其中。最初为咨询公众而发布的教程草案将进化论排除在外，但是看起来政府有时（如果并非总是）也听信科学家和专家们，这些科学家和专家们在英国人文协会的组织下签署了一封致Ed Balls（首相的贴身建言人）的公开信（pdf），要求将进化论包含在小学课程中。<br />
<br />
关注公众理性的人通常会被民意测验和调查所显示的对于‘智慧设计论’的高度信任所震惊。最近的调查似乎说明大多数英国人认为‘智慧设计论’应该与进化论一同在学校讲授。<br />
<br />
要解决这个问题，我们必须要了解起因，有两个原因可能使你倾向于智慧设计论多过进化论。可能因为你先入为主的思想信念――主要是宗教信仰――不允许你接受展现在你面前的事实证据。或者可能因为你真的不知道进化论证据，从未有人向你解释进化论，或者你就是不理解它。在英国这样宗教日益没落的社会，很难相信大多数不接受进化论的人是由于思想意识驱使而非无知。这就意味着最佳解决之道是通过更好的教育，因此我们得出结论：尽早在科学教程中包含进化论是相当重要的。<br />
<br />
其实进化论可以被看作一个有难度的概念，而且进化论的大多数资源是面向中学学生的。但是今年这个庆祝年发行的针对小孩子的大量新作，从What Mr Darwin Saw（达尔文先生之所见）到进化革命，甚或像How Whales Walked into the Sea（鲸鱼漫游海洋）或Mammals Who Morph（变种哺乳动物）之类的旧作，都表明进化论是一个容易令小孩子们愉快并理解的科目。这是件好事，因为进化论可以说是生命科学中最重要的概念，在孩子们尽可能小的时候向他们施加一种进化论的理解毫无疑问将为今后更科学的理解奠定基础。<br />
 <br />
我们一定要作此期望，因为即便在我们的国家导致进化论接受程度低的主要原因是无知甚过思想意识，仍然存在其他因素妨碍公众――尤其是年轻人――更好的理解进化论。这样的因素很多但至少有两点与向年轻孩子们推介进化论这件事十分有关。第一个是自然认知偏差（譬如我们人类寻找设计和目的的倾向性）的深刻性阻碍了对进化论的接受；很显然最好的反击方法就是让孩子们在尽可能早的时候接受进化论教育。第二个是由科学教育家James Williams精辟阐述的一种现实，即众多流行文化中的上帝造物论讯息劈头盖脸向年轻的孩子们袭来，而这些流行文化着实暗藏危害，令人迷惑。<br />
<br />
新版的初级课程，以及2007政府指南禁止在科学课上讲授上帝造物论和智慧设计论的禁令，会令英国学校处于进化论教育的前沿。值《物种起源》150周年纪念月即将到来之际，暨良好科学教育乃迫切大事之时，这实在是再及时不过的举措了。</div>

							
							
							
							
