<br />
简介<br />
基督教领袖们表示，外星生物的存在并不违背圣经教义。但在外星人世界里，是否有长着触手的基督式人物？或者地球的基督是通用的？<br />
译言联合3G门户推出无线官网，手机看译言，登录wap.yeeyan.com<br />
<br />
 <br />
梵蒂冈加入搜索外星生物行列&nbsp; &nbsp;图片来源：AP/20TH CENTURY FOX<br />
 <br />
 <br />
本周，科学家和宗教领袖聚集罗马，参加教皇科学院（Pontifical Academy of Sciences）举行的一场太空生物学会议。<br />
 <br />
在1600年意大利僧侣佐丹奴・布鲁诺（Giordano Bruno）因声称存在其它世界而遭到审讯并被处死之后的数百年来，神学家们就在太空生物的问题上争论不休。<br />
&nbsp;&nbsp;<br />
其它的暂且不说，单是看起来极像外星人的外国人就很难符合神“按照自己的形象创造了人类”这一说法。<br />
 <br />
而且，基督作为救世主的角色也会让人疑惑：在外星人世界里，是否有长着触手的基督式人物？或者地球的基督是通用的？<br />
 <br />
然而，正如教会最终接纳了伽利略有关地球并非宇宙中心的说法一样，当他们姗姗来迟地承认达尔文进化论中的事实时，基督教领袖们表示，外星生物的存在并不违背圣经教义。<br />
&nbsp;&nbsp;<br />
本次会议的组织者之一、梵蒂冈天文台基督会天文学家何塞・福内斯神父（Jose Funes）表示：“就像地球上存在各种各样的生物一样，神创造了外星生物甚至是智能生物也不足为奇。”<br />
 <br />
“这和我们的信仰并不冲突，因为神的创造力是无限的。”<br />
&nbsp;&nbsp;<br />
他的话并非人人信服。《金发之谜》作者、理论物理学家保罗・戴维斯（Paul Davies）向华盛顿邮报表示，教会领袖们低估了基督教因此会遭受的威胁。他表示：“我认为，发现另一种智慧生物具有巨大的精神意义。”<br />
 <br />
“外星智慧生物的发现（对基督教来说）是真正的威胁所在，因为如果宇宙中存在其它人类，那么基督徒们将陷入可怕的窘境。”<br />
 <br />
“他们相信，神化身基督以拯救人类，而不是拯救海豚、黑猩猩或者是其它行星上的绿色小人儿。”<br />
 <br />
在教皇科学院举行的这次会议上，科学家们――决不都是基督徒――将就一系列问题发表演讲，包括系外行星的发现，地球早期生命的地质记录，生命起源，以及在生物化学上不同于人类的未知“外星”生物是否存在等等。</div>

							
							
							
							
