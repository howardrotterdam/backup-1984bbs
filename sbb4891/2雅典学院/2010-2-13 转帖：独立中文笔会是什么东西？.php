													<h2>转帖：独立中文笔会是什么东西？</h2>
						
						
																											<div id="postmessage_479044" class="t_msgfont">最近一段时间以来，独立中文作家笔会闹开了锅。如果你不知道文革是什么东东，欣赏一下&quot;以刘晓波为谣言制做核心&quot;的那些&quot;作家&quot;你就知道了。详细拜读了那些内部发言，看见至少有&quot;四丑&quot;值得总结出来欣赏。这&quot;四丑&quot;不是指生理上长得丑（如果碰巧如此，实在属巧合），而是说那言行实在丑陋无比。总而言之，文革远远没有过去，还早着呢。<br />
&nbsp; &nbsp;一丑余杰。<br />
&nbsp; &nbsp;原因一：撒谎成性。主要案例是先以上帝名义撒谎排挤郭飞雄，后以基督徒的名义为排挤郭飞雄制造更多谎言。此外，为私利和私愤散步谣言诽谤高智晟、茉莉、任不寐、赵^、王朔、鄢烈山、钱理群等持不同政见者和著名作家。原因二：抄袭成性：此公无所不抄，思想，文字、甚至信仰，一直有证人证言在网络发布，此不赘。原因三：“残暴”成性，最早撰文要剥钱穆的皮，现在是谁不与刘余保持一致就诽谤开除谁。原因四：不择手段。一方面不择手段地恭维人，另一方面不择手段地骂人。目的只有一个，或者名利，拉帮结派，党同伐异，市恩买好，或者就是转移对自己的舆论压力。前者吹捧天An门母亲，以及一系列代为作文，吹捧一帮“美女作家”等等。后者就是现在以基督教代言人的名义骂发L供。李洪志相关言论（余引用的不完全属实，有撒谎成分）不是今天才有的，问题是余杰为什么今天才跳出来，因为今天他遭遇了前所未有的压力。此人在压力之下的习性是：找更大的目标转移公愤。善于恭维的人必然善于诽谤，余杰之丑，名冠笔会。<br />
&nbsp; &nbsp;二丑小乔（李剑红）。<br />
&nbsp; &nbsp;了解此女可问作家张南云。不过此人如今上榜不仅因为长期以来逢刘必捧的谄媚功夫，而是在笔会开除高寒事件以及郭庆海退会等事件上的表演。她先发文表示要珍惜笔会安定团结的大好局面，以及来之不易的和谐气氛，该文的文风立意可以在六四以后任何党报有关&quot;稳定压倒一切&quot;的社论中看到。小乔自身揭露了笔会现在到底是一个什么样的组织，这是这位无脑女士的贡献。另外就是在自自中国论坛调侃和咒骂退会的郭庆海。谁退骂谁，不退不骂，这个小宗派的流民或流氓无产者在小乔身上表现出色。那些对郭庆海最可笑的理由是：&quot;郭庆海&quot;作为&quot;独立作家&quot;没有什么象样的作品，以及不能与人合作。然而，独立中文作家笔会的作家们，特别是小乔，刘路、吴伟等等，特别还包括笔会编外著名网络作家刘荻等，有什么&quot;象样的作品&quot;么。这些半瓶醋如今因忠于刘晓波的缘故不仅是知名作家，还捆绑著名诗人黄翔一起按劳分配去领奖，你就知道这笔会有多大的能耐了。至于什么&quot;不能与人合作&quot;，这话反过来用在小乔为代表的笔会身上更合适，除非你说&quot;不能与人合作&quot;就是&quot;不能与我们合作&quot;，但那不正是人家离开&quot;我们&quot;的原因么？<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;三丑杜导斌。<br />
&nbsp; &nbsp;此公最近的一系列表演活托托一个网络五毛--根据他自己的交代或表白，刘晓波以及笔会授意他在一些公共论坛发言，有计划有任务地回应网络上的批评。这不仅意味着他丧失了基本的独立写作的立场，而是堕落为为立场而写作的&quot;网警&quot;。他必须逢批必反，而这些反应还不是因为从他自己的角度看值得商榷，而是因为刘会长交代的任务。最近几天令人难忘的作品是批评高寒是文革余虐的妙文。如果你不知道什么叫文革余虐，你去看此人这篇文章就行了。具有讽刺意味的是，他竟然打着文革反文革。杜导斌代表了刘晓波时代所谓独立作家是什么人，他们根本不是为正义独立写作的，而是为利益拉帮结伙写作的。刘晓波成了董事长，稿费按忠心分配。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;四丑刘路/李建强/康平/李晓蓓。。。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;律师不懂法律，诗人最多是梨花水平，但伪善功夫直追刘晓波。排郭丑闻后此人先以爱护郭飞雄的名义连写文章为余王辩护，在舆论压力下，又由刘晓波授意负责牵头组织文章&quot;回击任何对笔会的攻击&quot;。与此同时，与刘荻联手，不断变幻马甲在刘晓波的论坛上攻击高智晟。前不久暴露目标公开为党中央提供司法辩护，指控高智晟&quot;由反共走向反华，堕落成了国家的敌人&quot;。此举不仅触发众怒，而且将刘晓波及笔会的以利润为第一目标的本色揭露出来。这造成了刘晓波的被动因此受到&quot;党内批评&quot;。为了挽回经济损失，此番在笔会上再度表演了&quot;律师/诗人&quot;双丑角色：如果法理上不堪一击，就开始玩诗人的&quot;自恋&quot;。有人说刘路为笔会军事，那是抬高他了，他不过就是一个渴望分赃者。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;上面四丑为什么不包括刘荻，那不仅是因为她只是笔会的党外友好人士，更因为她丑得实在超出界限了，已经达到了以丑忘我的境界，非笔力所能形容。另外值得一提的是刘晓波的铁哥们&quot;著名作家&quot;廖亦武，一句对笔会敌人高寒&quot;徒具人形&quot;的高论（刘荻马上附和说此评还是高了），足以显示&quot;著名作家&quot;是怎样著名的。至于郑义、草虾、浦志强等友军，我怀着十二分的诚意奉劝他们去一些咨询机构，总被自己文字和言辞感动，那不意味着别人一样这样感动，以至忘记了牵一利而动全身的基本事实。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;最后得说说刘晓波会长，这位真正的丑角或丑闻制造商--他是所有丑闻的收集整理编造和批发者，并是上述丑角的鼓动者和买单者。可以公平地说，刘晓波已经成功地将独立中文作家笔会变成了一场政治丑闻，而且一直在把异见写作变成一系列经济丑闻。刘晓波到底是什么人，最近明显的一系列&quot;良知勇气&quot;之类的双重标准的文章显示了一二。八十年代的那匹黑马，首先是一位不择手段成名的青年教师，这从他咒骂李泽厚等人看出来。有人美化刘晓波八九年&quot;毅然回国参加民运&quot;的神话，事实上，在那热火朝天的时候，任何人都&quot;敢于毅然回国&quot;。刘晓波的勇气神话一进监狱就在官方媒体上显示出来，这次忏悔对虚荣心极强的刘晓波是极大的打击。这种打击导致刘晓波第二次入狱以挽回损失，同时造成了刘晓波与周舵的分道扬镳，并使他出狱以后一段时间里大骂海外民运的逃跑主义。此番及其后的勇气却也不乏可圈点之处。在刘晓波出狱的两三年里，刘晓波、任不寐、余杰、王怡所谓的网络四君子一度在网络上开辟了异见写作的新气象，当时整个反对运动也空前团结，写作水平和道义形象都非常令人鼓舞。但是，好景不长，先是任不寐退出笔会，网络写作的黄金搭挡时代开始瓦解，任不寐退会同时使自己演变成刘晓波为代表的笔会的公敌。这是第一步。接下来，第二步，反对运动进一步大分裂是余王排郭事件，背景是刘晓波与高智晟之间愈演愈烈的互相诋毁。这一风波造成了笔会和维权运动的公开决裂，这场冲突从国内席卷国外，老于世故的保持中立，但基本上都以不同姿态参与进来了。这个裂痕恐怕难以弥补，而郭飞雄回国即被抓捕，这一悲剧也将反对运动放在悲剧之中，使刘余王成为被告。第三步，包括很多细节，如廖亦武骂袁红兵造成另外一起纷争，丁子霖批高智晟率先离析了反对运动，余杰报复性咒骂赵昕范亚峰等将冲突进一步升级，刘晓波和丁子霖乘杨建利出狱之机，在在成功夺取《民主中国》编辑权利之后，试图进一步夺取议报的舆论阵地，就是进一步剥夺任不寐在议报的主编职务。还有就是丁子霖刘晓波余杰王怡等与四川天网及成都读书会之间的冲突，这些冲突以丁子霖刘晓波公开指责另外一位天安门母亲&quot;投降&quot;为高潮。等等。最后一起事件是余杰挑起和酝酿中的基督教和发轮功之争，背后是长期以来丁子霖、刘晓波、吴弘达（余杰以及王怡等主要的海外资助者）与发轮功的各样恩怨。此外，还包括刘余等联手逼盛雪退出理事会，造成了刘余和郭罗基、盛雪等人的分裂。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;在所有上述这些冲突中，刘晓波和他废寝忘食的电话扮演了决策性的角色，他是整场混乱真正的推手。可以这样说，所有的流言、谣言、偏见，都是经过刘晓波刻意收集处理和转发的（刘荻承担了主要助手的角色）。问题是，刘晓波的目的到底是什么？无非名利二字。刘晓波和任不寐的冲突首先是因为任不寐在性格和写作优势方面，不可能成为刘晓波宗派的一员。而刘晓波和高智晟的冲突首先起源于嫉妒，背后就是资源竞争。高智晟的朋友郭飞雄范亚峰等的故作政治成熟等等姿态，进一步刺激了刘晓波的斗志，丁子霖的公开信，以及余王拒郭，都是从刘晓波那里得到信息和&quot;灵感&quot;的。刘晓波和袁红兵过去有私人性情上的冲突，但问题是，是刘晓波而不是袁红兵把私人评价上升到政治攻击的。刘晓波一定要把自己的道德标准、写作风格和政治模式强加于人，他这种儒家人格比他批评的任何专制人格都要专横和缺乏自知之明。丁子霖是另外一位道德自我感觉良好者，她的感觉因苦难更无法理喻。这造成了两位&quot;圣人&quot;或&quot;君子&quot;联手领导的对各样&quot;小人&quot;的文革运动。高寒之所以成为这&quot;小人&quot;中的最著名的受害者，刘晓波一定要开除他而后快（再没有比&quot;开除高寒&quot;的闹剧更有丑剧和戏剧效果的了），就是因为狭隘和仇恨--刘晓波最不能容忍的不仅是别人比自己更优秀更出名，而是他竟然支持那样的&quot;敌人&quot;。最后的理由就是利益，刘晓波等人，这些号称自由化主义者，号称了解自由主义精髓的著名作家，他们既然已经把党的利益原则分析的头头是道，现在也应该用同样的原则来分析分析自己了。如果说共产党的专制政治的基础是利益，那么，刘晓波们的&quot;自由写作&quot;的基础也同样是利益。否则就有两个标准之嫌。什么是刘晓波们的最高利益，就是尽可能地在单位时间里生产更多的文章兑换更多的稿费和更多的名声。但要达到这样的目的，必须保障两个条件：第一，尽可能垄断发表权，第二，必须保证文章的风险不能超过一定的界限--决不能进监狱。为了创造第一个条件，刘晓波必须党同伐异。为了第二的条件，刘晓波必须与激进主义划清界限。这有两个结果，第一就是刘晓波必须在文章中对共产党和其他民运的政敌采取两个评价标准，对共产党的批评&quot;更理性&quot;，对民运的批评更激进，他因此可以容忍共产党的激进主义独裁，却无法容忍或表现得无法容忍民运的激进主义抗议。事实证明，民运的激进主义基本上是刘晓波虚构出来的，正如勇气和良知的二元论是他虚构出来的一样。如果说真有这种二元冲突，是刘晓波而不是别人，恰恰代表着这两种矛盾性格，这矛盾的性格统一在利害算计和嫉妒之中。第二个后果就是生产了大量的网络文字垃圾。刘晓波以及余杰等，因为稿费的缘故，根本不考虑文字质量，盲目生产，千篇一律，写作成了表态，道德成了卖钱，以至海外所有异见媒体都成了他们的垄断市场。刘晓波在经营自己的利润的时候很聪明，就是拉住丁子霖以令海外，网罗一些无套裤汉（&quot;网络作家&quot;）以小名小利组成群众基础。接下来就是到处市恩买好，利用人性弱点鼓动围攻&quot;小人&quot;。到目前为止他基本还成功，但是不全成功。而且似乎已经露出败象。<br />
 没有必要让刘晓波为近年反对运动的混乱负责，尽管他是祸根。因为路都是每个人自己走的。每个人都该反省，无论看客还是打手。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;但是，刘晓波代表着中国文化和党文化深刻伪善的那些东西。他有一定的道德激情，但他的利益激情比别人更发达。成为笔会会长以来，刘晓波与任何商人没有区别，他就是要不择手段的赚钱，而且要理直气壮地比别人过得好。这本无可非议，问题是，这位商人为此却要把自己的名利建立在别人的耻辱之上，为此不择手段，这就意味着，他所经营的事业有一个内部无法克服的矛盾：他赚钱靠的是道义和文字，但却必须通过越来越不道义或伪道义的方式，以及生产越来越多不忍卒读的文字垃圾。刘晓波自己没有任何办法克服这个矛盾。他和他的宗派只能越来越丑。不过也许他有一天真能好好反省反省，完美主义人格（这是他一直批判的专制人格）再加上激进主义的利益趋向，给他所毁灭的人带来了怎样的灾难，而给自己究竟带来了哪些&quot;利益&quot;。刘晓波现象不是一个人的问题，从他身上你就知道，中国实现民主为什么不可能。刘晓波比共产党更深刻地告诉我们中国文明与西方文明之间的真正差距，这不是五十年可以追上的。</div>

							
							
							
							
