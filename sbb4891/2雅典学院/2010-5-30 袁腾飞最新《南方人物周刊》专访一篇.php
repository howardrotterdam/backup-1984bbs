													<h2>袁腾飞最新《南方人物周刊》专访一篇</h2>
						
						
																											<div id="postmessage_647744" class="t_msgfont">来源：《南方人物周刊》 <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;高、平头、小眼睛、胖瘦宜人的中学历史老师袁腾飞“非正常成名”之后，战战兢兢，如履薄冰，不但“夹着尾巴做人”，而且“两头三天换号”。他坚决否认如下称号：史上最牛中学历史教师，他表示“愧不敢当”，“北京2500个中学历史教师，很多很优秀”。<br />
<br />
　　他不是妖孽，也绝不麻辣，宅男一枚。喜欢独处，过度关注会令他腼腆。他任教的精华学校的丁老师说，其实袁腾飞很低调，“从走廊过去都轻轻的”。<br />
<br />
　　每次讲课，他都会把讲台仔细擦干净，课本绝对干干净净。他喜欢历史，对历史亦怀有温情和敬意，绝不敢放肆。他说，读史对他来讲只是一种“生活方式”，“不让我看书我不知道一天到晚干什么。”<br />
<br />
　　去年，他的讲课视频被放上了网，点击暴高，有学生说，你应该上《百家讲坛》。他说，不可能，开玩笑!当《百家讲坛》真给他打电话的时候，他很诧异，马上就拒绝了。对方说你再好好考虑一下，试讲一下。他去试了，一讲就刹不住车了，录了30讲，1200分钟。讲完《两宋风云》，又讲《塞北三朝》。<br />
<br />
　　到如今风生水起，有点闹哄哄的，他立场坚定，“咱不是娱乐圈的人，也不是季羡林。”“讲的东西可能有不规范不严谨，因为我不是历史学家，一名普通的中学历史老师，带动大众去看历史了，这就算是功德事了――就跟京剧下校园一个道理。”<br />
<br />
　　“你懂的人家也懂，人家说凭什么你交了狗屎运了?所以我就变得不张扬了，本来早上10点钟去上课的，现在得9点就去。外边人都是很不明真相，说，你天天上电视，上一次怎么也得给你几十万吧?我的天，几百都没有哇!现在上春晚都要花钱啊!”<br />
<br />
　　他也不喜欢辩论，不愿意跟任何人产生争论，不愿意跟任何人产生冲突。 “那是特无聊的事。”又补充说，“不利于和谐。”<br />
<br />
　　他这么说男人，“真正的男人，敢担当，有才气，有理想，有抱负，敢爱敢恨，大概就是这样的。”至于读历史给他带来什么好处，“读历史的人，一般想得比较开，说得不好听的话，自杀的比较少。”他崇拜文天祥、岳飞，时常说，“你冤，冤得过岳飞吗?”<br />
<br />
　　后来因为版税问题，和出版商“干”了起来，5月24日对簿公堂。这都不是他期待的。<br />
<br />
　　“我怎么混到这个田地了呢?”他说，“我真想回到从前的日子，真不是跟你矫情。”<br />
<br />
　　《历史是个什么玩意儿》这个名字，不是他取的。他反感炒作。书的腰封写着：袁腾飞=易中天+郭德纲。袁腾飞没那么不羁，他老老实实地说：“我很不认可，那都是一种炒作，不是我自封的。”<br />
<br />
　　他坚决地给自己定位：一名普通的中学历史老师。以一个读史人的身份，粗糙地反抗着魅力，“我不是公共知识分子。”<br />
<br />
　　最为喜感的是，当传说他被警方逮捕的时候，他在视频上一本正经地说：请大家相信党，相信政府。<br />
<br />
　　你站在什么阶级立场呢?<br />
<br />
　　袁腾飞生于1972年，在中学连个组长都没当过，刚参加工作时，也不会管人，“那时正是读书无用论最猖獗的时候，认为会外语、会电脑、会开车，这就属于人才，历史课本从来就不好教”，“按部就班地讲课，就老乱堂。”<br />
<br />
　　“那个时候的孩子不听你的课，学校又是那种特别破的楼，都不隔音――不像今天的学校都有钱了，校长和教学楼都隔着八丈远――那时校长一听你这屋子怎么回事?你的饭碗就保不住了。”<br />
<br />
　　“那时候找个工作也不容易。1994年，国家还是分配工作。师范类学校毕业不服从国家分配的话，就得赔钱，两三万呢!那时候做校长一个月才800多块，两三万不是天文数字嘛，所以只能服从分配。”<br />
<br />
　　“1994、1995年，教十二三岁的孩子，真管不了，累。学生闹，我说你们这帮孩子寡廉鲜耻。现在你们是乐了，那时他们说，老师这个寡廉鲜耻是什么意思啊。我说，就是臭不要脸。你让他背鸦片战争中国失败的背景、经过、原因、评价，他能背得下来吗?”<br />
<br />
　　“正史很好看，很有趣，很逗，教科书不好看。所以我就希望教科书能用孩子听得懂的语言去写。”<br />
<br />
　　他恪守本分，缺乏野心，在据说是重点学校的首师大附属中学教了15年才调入北京海淀教师进修学校，不想加入传统的史学圈，“最烦考据”。<br />
<br />
　　目前为止，他算是一名好老师，自己着正装上课，连牛仔裤都不爱穿，他和孩子们“不是一个朝代的”，演艺圈之八卦、炒股于他更是天外飞仙。他讲课眉飞色舞，却不大管学生。 “困了你就睡，饿了就吃，饼干、面包、巧克力，喝水我不管，泡泡糖、嗑瓜子这不行;做数学作业我也不管，下棋就不行。传条不行，你不会连短信都发不起吧?发短信不影响别人嘛!”<br />
<br />
　　5月22日，原定在北京某书店的袁腾飞签售取消了。第二日，5月23日下午，精华学校原定的袁腾飞的课也取消了。5点下课时，一名十六七岁的女学生一边走出来，一边对前来接她的母亲林女士说：“他们太欺负人了，我要去声援袁老师。”<br />
<br />
　　林女士说，“袁老师很冤枉。其实他上课的时候，都会用辩证的方式授课，给学生全面客观的观点。但是针对他的人抓住只言片语就拼命攻击，这让我想起了我们经历的文化大革命。”<br />
<br />
　　他的一名学生这么评价他：袁老师是一个杰出的班主任，一个真正为学生着想的好老师。又说，“能让学生喜欢上自己所教的学科的老师就是好老师。”<br />
<br />
　　20岁的时候，袁腾飞问老师：“马克思主义5种社会形态，太平天国属于哪种?老师说，属于封建政权。袁腾飞说，太平天国是封建政权，清朝也是封建政权，俩封建政权，为什么天平天国就是正义的?清朝就是落后的?”<br />
<br />
　　“老师这么看着我，我记得倍儿清楚，这么看着我(侧脸做了个眼神)：你站在什么阶级的立场上说话呢?”<br />
<br />
　　不会开着宝马车去上课<br />
<br />
　　袁腾飞成名后有些身不由己，“不是讲座，就是应酬，不想看的非让你看，想见的人不安排你见。”他去了某市，“仿古一条街，就表演歌舞，问我说，我们的歌舞精彩不精彩。我说，怎么推出两门大炮来?宋朝没有大炮，最早的都是元朝才有火铳。他们说没关系，就为了好看――那你叫我来干嘛呀?”<br />
<br />
　　他一张嘴就是北京大老爷们的话音：“我这个人呢特别懒，因为有糖尿病，不爱运动，能躺着不坐着，能坐着不站着，张家界我爬不上去，就比较爱看人文。你看庙，哪个庙比雍和宫棒?金碧辉煌。西藏咱没去过，那可能更棒，但咱上不去，到那儿憋死了;你看建筑，哪个建筑有故宫好?然后你说看园林，哪个园林有颐和园棒?但我一看三孔(山东曲阜的孔庙、孔府、孔林)，不得了，不逊于北京故宫。还有南京，不愧为六朝古都，中山陵太棒了。”<br />
<br />
　　他喜欢跟团去欧洲，因为“欧洲人活得特自在，阳光，不像咱们这苦大仇深的。人家蓝领就是蓝领，绝对不会说考个硕士，将来当白领。五星级大饭店点一杯啤酒在那儿坐着，照样很开心，没有人歧视你，不会说最低消费200等等。不在乎开什么车，拿着钱去南极滑雪去，去沙漠挖死尸去，干这个，我高兴。”<br />
<br />
　　说着说着就高兴了：“我这个人就是二皮脸，去欧洲旅行，多烂的英语我都敢说，买衬衫，这是长袖还是短袖的一比划就完了，会几个词就完了。”<br />
<br />
　　他不怎么上网，推特上的、微博上的袁腾飞不是他，博客上的他也不是他。他交友的圈子也很小，“君子之交淡如水，没有闺密。基本上在一块儿都是聊200年前的事，要不就聊100年后的事，不怎么着调那样的。”<br />
<br />
　　他说，他喜欢外国史、古代史，中国现代史他不爱讲，因为可展开讲的余地不大。<br />
<br />
　　他自称没什么梦想，理想却已经实现了，就是“爱干嘛就干嘛”，不苛求物质生活，不会开着宝马去给中学生上课，也不可能拿一个LV包去，“学生该问你了，你哪儿仿的?仿得挺像的。”“广厦万间夜眠不足五尺，粮田千顷日食不过三餐，何必呢?你说你们家12个卧室，你能一晚上挪12个地儿吗――你得有完啊。”<br />
<br />
　　为了好玩，让学生爱听<br />
<br />
　　人物周刊：你是一个历史老师，而你的受众是应试的学生，以及非历史研究者的普通观众，对吗?<br />
<br />
　　袁腾飞：对，我是为了好玩，让学生爱听。我举外国例子吧，中国例子不好说。法国大革命的时候，上断头台的法国王后，很多人觉得她很冤，认为她罪不当死，因为她骄奢淫逸，爱乱花钱，她又不是像现在的贪污犯，贪污的是国家的钱，她是专制的时代，国家是她的，她花的是自己的钱，没有哪条法律能够判她死刑――那怎么能够让中学生明白呢?<br />
<br />
　　你就说她就是一个爱花钱的傻娘们嘛，爱瞎糟，学生哈哈一乐就完了。奢侈不能判死罪，就这么个意思。<br />
<br />
　　人物周刊：一种东西，有几种不同的讲法。<br />
<br />
　　袁腾飞：比如讲科举制度。宋朝的皇帝自己写诗，他告诉下面的人要好好学习，书中自有颜如玉、千钟粟、黄金屋，车马多如簇。同样是这一首诗，在《百家讲坛》讲，就书面语，多用点4个字的词，用点排比句，念念古文什么的，正面讲什么叫车马簇，什么叫千钟粟，就完了。给中学生讲，你得逗逗他，比如说，博士就比硕士工资高，硕士就比本科工资高，如果你连大学都没有的话，别说千钟粟、颜如玉了，连棒子面、柴火妞都未必有。跟公务员讲，就得讲科举考试跟今天国家公务员考试是一种什么关系，就讲出一点厚度来。<br />
<br />
　　人物周刊：你说宋朝是一个很可爱的年代，经济文化繁荣，还是一个知识分子特别幸福的时代。<br />
<br />
　　袁腾飞：如果一个国家知识分子是最穷的，那我不敢自称是文化大国，宋朝就是一个文化大国，它的知识分子是最富的，所以皇上告诉你，书中自有黄金屋、颜如玉，这不是吹牛。你确实做了官就有这些，真的是知识改变命运。如果说我苦哈哈的，全家借债我上一个大学，然后上大学欠了一屁股债，完了出来找了一个工作就是超市送水，那我为什么要读大学?如果知识不能创造财富的话，怎么能够告诉别人知识有用?<br />
<br />
　　宋朝真的是一个知识创造财富的朝代。你看苏东坡他穷吗?光媳妇好几个，白居易(唐代诗人)穷吗?他们家蓄妓，他能穷吗?你跟皇室亲王贵族比，当然是不行了，一般是中产阶级往上肯定是没问题的。苏东坡流放岭南的时候当然穷了――他流放岭南还带着小妾去呢。<br />
<br />
　　中国古代这种君臣共治，臣相对皇权进行制约，发展到极致的就是宋朝。个人的自由，最起码士大夫这个阶层的自由度，文化体系对皇权的牵制足够大。<br />
<br />
　　比如宋朝的第三个皇帝，宋真宗，想知道国库里有多少钱，宰相就告诉负责国库的官员，不许告诉皇帝，怕皇帝知道国家有钱，乱花钱。宋朝文官士大夫是没有死刑的，有一个官员贪污，皇上说他真该杀。宰相就说，祖宗家法不杀士大夫。皇上说，咱流放他。结果宰相说士可杀不可辱，不能流放。皇上说，如此说来，快意事朕一件也做不得?宰相说，这样的快意事不做也罢。皇帝不是为所欲为的。<br />
<br />
　　人物周刊：电视里皇帝不是经常把人推出午门斩了的吗?每次去午门都觉得脖子凉飕飕的。<br />
<br />
　　袁腾飞：明朝杀人是在西四，清朝是在菜市口，午门不是杀人的地方。再有一个，中国古代的皇帝不是想杀大臣就杀大臣的，也是有法律的，他也是法制国家，不是说皇上想杀谁就杀谁。<br />
<br />
　　像宋朝最高的死刑就是斩首，没有比斩首更厉害的，但是明清有凌迟的，尤其是明朝弄点事就凌迟，很多的，我觉得明清就很恐怖。<br />
<br />
　　人物周刊：咱们中国五千年文明，是否乱世出英雄?<br />
<br />
　　袁腾飞：中国文化的高峰、大师辈出的时代都是乱世，先秦诸子百家，魏晋南北朝，包括唐安史之乱，到宋文学都有，然后就是民国的时候。民国中国的教育家涌现，为什么?因为政府顾不上管你。你骂蒋介石，蒋介石有工夫理你吗?那儿好几个军阀造反呢，毛泽东那儿起义呢，哪有功夫理你啊?国家大一统了，政府才想着治你了。</div>

							
							
							
							
