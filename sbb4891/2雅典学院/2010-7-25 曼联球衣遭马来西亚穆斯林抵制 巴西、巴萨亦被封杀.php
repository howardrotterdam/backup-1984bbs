													<h2>曼联球衣突成大马穆斯林禁品 巴萨巴西亦遭封杀</h2>
						
						
																											<div id="postmessage_758570" class="t_msgfont">2010-07-21 10:01:58　来源: 网易体育　跟贴 35 条 手机看赛事 <br />
<a href="http://sports.163.com/10/0721/10/6C4054D600051CCL.html" target="_blank">http://sports.163.com/10/0721/10/6C4054D600051CCL.html</a><br />
<br />
<img src="http://img4.cache.netease.com/sports/2010/7/21/2010072110001157d2e.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
马来西亚首府柔佛的伊斯兰教理事会和霹雳州伊斯兰教组织都发表声明表示，十字架、酒类品牌广告和足球衣上的魔鬼标志，对真主都是亵渎，穆斯林不得穿着。<br />
<br />
曼联在马来西亚是最受欢迎的队伍，而大马多数人都是穆斯林，因此，对球迷而言，这或许已经成为一个两难的选择。去年，红魔访问马来西亚，两场对阵马来西亚明星队的比赛，上座人数都超过4万人。<br />
<br />
除了曼联，巴西、葡萄牙、塞尔维亚、巴萨和挪威的球衣也被禁穿，因为他们的队徽都有十字架。霹雳州伊斯兰教领袖、拿督贾铎表示：“没有任何借口穿这种服装，因为作为穆斯林，这意味着你崇拜了另一种宗教的标志。在这个问题上，绝对没有回旋余地，我们不能向娱乐、时尚甚至是体育妥协。”<br />
<br />
霹雳州宗教司哈里山尼则补充指出，穆斯林穿这种球衣“就是走上犯罪之路”，因为在服装商展示其他宗教信仰的标志，意味着这个人信奉其他教会胜过了伊斯兰教。<br />
<br />
<br />
今年3月，曼联还跟马来西亚电信签订了5年赞助合同，成为大马最受关注的体育组织。“任何参与了我们去年夏天远东之旅的人，都知道马来西亚对曼联的感觉。”曼联总裁大卫・吉尔当时评价道。<br />
<br />
不过，伊斯兰教的决定，已经遭到马来西亚球迷的猛烈炮轰。“很快，他们会把算术里的+号也改成x，因为那个符号不是伊斯兰教允许的。”一个球迷在网络上讽刺道。<br />
<br />
过去，马来西亚一直是个温和和改革派的穆斯林国家，然而，现在情况有了改变，1月份，当地发生焚烧教堂的行为，2月份更是宣判三名通奸的伊斯兰妇女笞刑。<br />
(本文来源：网易体育 作者：junior)</div>

							
							
							
							
