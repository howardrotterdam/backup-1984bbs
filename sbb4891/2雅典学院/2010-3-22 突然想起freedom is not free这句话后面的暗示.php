													<h2>突然想起freedom is not free这句话后面的暗示</h2>
						
						
																											<div id="postmessage_526066" class="t_msgfont">就当说个笑话，freedom is not free，是不是在暗示freedom不是一种“权利”(right)，而是一种“特权”(privilege)呢。<br />
<br />
如果自由是一种权利，那显然不应该有价的。如果freedom is not free，那就是在暗示freedom本身是有价的，有价，就是说可以进行交换 -- 比如在特定条件下，人们可以用freedom来换取面包。如果承认这一点，可能引发的伦理危机之一，便是很多极权主义思想就可以由此寻找到伦理支撑点 -- 就像土共诡辩的那样，人权首先是生存权，中国由于种种条件所限，为了谋生，不得不付出一些freedom作为代价。这就如同为了保障freedom，有些人不得不付出生命作为代价一样。既然可以用生命来换取自由，自然也应该允许有人用自由来换取生存喽？<br />
<br />
老茅在《请给你爱的人以自由》里，隐隐约约的提到了right和privilege之间的这种复杂关系及其可能引发的困境， 他还提出right和privilege的内涵是有条件的，某些情况下是可以相互转换的 -- 比如一般情况下，呼吸空气可视为一种right，但在资源极度稀缺的极端情况下，比如太空船上，呼吸空气可能就会成为一种特权privilege，成了特权，就意味着将成为一种需要通过交换以市场手段来定价的商品。<br />
<br />
那么，以这种眼光来考量当今的中国，很多我们视为right（应该）不可动摇无条件的东西，比如“司法公正”，有没有可能仅仅是一种假象呢？由于某些客观原因，比如资源稀缺，社会无法负担是司法公正成为一种right的代价，无法让社会所有成员都享受到，于是司法公正就只好成为一种特权，“衙门口朝南开，有理没钱莫进来”其实描述的不过是司法公正成为特权在市场上寻租价高者得的一种事实？<br />
<br />
司法公正如此？自由本身呢？<br />
