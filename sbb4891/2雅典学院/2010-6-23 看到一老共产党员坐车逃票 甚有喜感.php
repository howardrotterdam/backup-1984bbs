													<h2>看到一老共产党员坐车逃票 甚有喜感</h2>
						
						
																											<div id="postmessage_698516" class="t_msgfont">刚才赶上末班车顺利到家，遇到件喜感的事儿。<br />
<br />
我坐的公交车本来是有买票员了，最近刚实行无人售票，有些人习惯从后门上了，司机也就提醒你到前面刷卡投钱。一般人多的时候就请其他乘客帮忙把车钱或公交卡从后门传到前面再传回去，这是再正常不过的事儿了。<br />
今天我坐车的时候就有两人趁着从后门下客的时候由后门上了车，司机自然也开始提醒后门上车的两位投币刷卡。车开了一会儿，司机喊了几次都没反应，有的乘客就看不下去就说了，你坐车哪能好不买票？人家司机叫了这么多次了，你不方便走到前面去，其他乘客会帮你传卡的。后来就争起来了。<br />
<br />
刚开始我站在前门塞着耳机打铸剑物语，懒得听他们争执。后来隐约听到说党员什么的，觉得有料，于是拿下耳机一边围观一边继续打游戏。不肯买票的是个上海老头儿，但也没到可以拿老年卡免费坐车的年纪。和他争的也是个上海中年男人。老头到底为啥不肯买票也不肯让别人帮他传钱我不是很清楚，我就听见他理直气壮地说“我是党员。”这下么好了，变成群众教育共产党员了。那个中年男人看他年纪大语气不好太凶，就语重心长地说共产党员就能坐车不买票？现在阿拉上海在开SB会，你们共产党员更要以身作则丫！等等等等……当然，在我听来是P话，完全没有讲到点子上。而那个老头张来张去还是那几句话，我是个党员……<br />
<br />
后面你一句我一句在争，前面乘客议论起来就没这么客气了。说这种事情也只有共产党员干得出来，平时贪污的事情都是他们做的，坐车不买票算啥？司机也说，就是有这么几个人2块钱的车都要逃票，几张面孔都认得出来了。还共产党类，共产党算老几啊？我听了之后甚感欣慰。<br />
虽然只是一件小事，但是我很愿意见到通过这种真实生动的方式给一车乘客上了一课，见识到一个野生共产党员的思维逻辑。其实这老头看起来也挺寒碜，两块钱都要省，应该是个底层的群众党员。虽然没有捞到组织半点好处，但是早已习惯成思维了，认为共产党员做的事就是正确的。<br />
<br />
当然我不是指党员都喜欢逃票，其实正常情况下，一般的共产党员也不至于坐车逃票。但是这老头儿不买票还搬出自己是个党员，真是匪夷所思……<br />
我又想到了电视上看到过一新闻，司机开车违章被交警拦下，交警让他拿驾驶证，他拿出了一张党员证，弄得交警哭笑不得。<br />
这年头不少行恶之人都知道搬出裆组织撑腰，我觉得这样很好，可以帮助其他公民早日认清裆组织里都是些什么东西。</div>

							
							
							
							
