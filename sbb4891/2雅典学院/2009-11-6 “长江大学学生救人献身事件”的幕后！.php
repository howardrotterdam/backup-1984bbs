													<h2>当时在场的人的陈述</h2>
						
						
																											<div id="postmessage_791131" class="t_msgfont">郝建国对《挟尸要价》相关报道的最新声明<br />
――兼回复长江大学宣传部长李玉泉<br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;2009年10月24日，湖北荆州宝塔湾。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;当地长江大学、长江大学文理学院（独立学院）两所高校学生何东旭、方招、陈及时为救两名落水少年献身。但是，在打捞英雄遗体阶段，却上演了一出“挟尸要价”的丑剧。作为调查采访此事的一名记者，这里将本人了解到的相关信息公开，供业界、学界以及社会各界参考。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 一，大学生救人与被救基本事实<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 2名落水少年，陈天亮是大学生救出的，另一名叫张志鹏的少年抓住渔夫伸出的竹篙爬上船获救。现场救人的韩德元、渔夫讲述了这一点，一名在场的同学也承认张志鹏是“大家合伙救的”。这个“大家”包括大学生、冬泳爱好者、渔夫。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;渔夫参与了救人。具体事实为，向江中扔过一只救生圈，伸出竹篙，有一名大学生、一名落水少年抓住篙爬上船得救。同学们向多家媒体讲述的“见死不救”，是因为他们混淆了“救人”和“捞尸”的区别，把事发现场的2只渔船和事后赶到、以牟取暴利为目的2只打捞船当成“一起的”。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;当然，可以肯定的是，渔夫的“帮助”或“参与”救人远不可能像大学生那样奋不顾身，他们救人不够积极也是被目击者证实的。但如果说“见死不救”，我认为有点过。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;我理解，这除了渔夫和大学生们的价值观、道德水平有差异之外，可能是因为渔夫对溺水和死亡见得太多，有点麻木。渔夫自己则称，他们晚上打鱼，白天在睡觉，所以没有在第一时间救援落水少年，但他们参与救援了大学生，而且没有收取任何报酬。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 二，学生遗体打捞基本事实<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 捞尸者是在3名大学生沉入水中20-40分钟之后赶到宝塔湾的。平心而论，此时3名英雄根本没有生还的希望，所以也不存在“见死不救”。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;捞尸阶段，本人通过现场学生、冬泳爱好者、现场目击者（照片《挟尸要价》拍摄者张轶，时任《江汉商报》摄影记者，后因公开身份被迫辞职）、警方等信息源，了解到的基本事实是：捞尸者赶到现场后，不问英雄的死活，不顾现场师生的感受，趁火打劫，开口就要钱，称捞一具遗体12000元，钱不到位不捞。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;当时校领导身上所带现金不够，答应对方先捞人，剩余的钱随后补上，但捞尸者不干。其间有女同学“跪求”打捞船船主尽快救人，但对方坚持要钱。无奈，师生们掏出身上所有的钱，凑了4000元交给对方，捞尸者才开始行动，同时扬言：“钱不到位的话，只捞一个”。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;图片定格瞬间画面是，捞尸者以答应的36000元还没有全部到位为由“罢工”，一个叫王守海的捞尸者，把打捞上来的方招的遗体用一条绳子绑住右手（图片上绳子打结很清晰），用另一条带铁钩的绳子钩住T恤，久久将遗体横在水中，而不是拖到船上。当时岸上的同学们哭喊着抗议这样的行为，但王守海摆手不动（请注意王守海的表情和手势，当时校方答应的36000元尚未全部到手）。王守海等人这样侮辱英雄遗体，目的是调动岸上老师和同学们怜悯和痛心，以及时拿到他们开出的36000元天价捞尸费。<br />
<br />
&nbsp; &nbsp; 李玉泉把令人发指的“挟尸要价”粉饰成“牵尸靠岸”。实际情况是，当时方招的右手和T恤已分别被两条绳子固定，绳子的另一端分别由捞尸者王守海还另一位穿蓝色衬衫的男子牵着，完全有条件把遗体拖到船上，不存在岸上配合的问题。《挟尸要价》引起大家愤怒的是，捞尸者“漫天要价”和“拿不到钱不捞尸”（学生将捞尸者误解为“见死不救”，当地警方调查的结论是“中断打捞”）的行为。<br />
<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;打捞3具遗体，捞尸者分3次共收取了学校36000元现金。《华商报》、《新京报》、《南方周末》等媒体将以上事实公开后，捞尸者最终将收取的36000元捞尸费全部退还学校（这个处理有点矫枉过正，捞尸收取一定费用有其合理性，属于正当民事行为，问题不是出在是否收费，而在于“漫天要价”和“拿不到钱不捞尸”），“挟尸要价”始作俑者陈波，因构成敲诈勒索，被公安机关处治安拘留15天，并处1000元罚款。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;大学生舍己救人遭遇捞尸者挟尸要价事件，触动了我们每个人的灵魂――大学生以生命为代价的利他主义与船夫挟尸要价、唯利是图的反差，暴露这个时代道德的分化和利欲的膨胀。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;当时并不在现场的长江大学宣传部长李玉泉，事隔近一年时间后，不顾英雄家属和长江大学数万师生的感受，突然冒出来为几个千夫所指、已经受到法律制裁的捞尸者“招魂”鸣冤，称记者“误读照片，制造假新闻”，无疑是继“挟尸要价”事件之后又一大丑闻！<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;李玉泉称，因为“挟尸要价”的报道，“人们在网上发贴咒骂‘渔民’的没有人性，甚至咒骂‘荆州人’的‘冷血’”。看的出来，李玉泉的表演是想粉饰社会上客观存在的丑恶，为所谓的“荆州人”正名。但这恰恰表现出李玉泉的荒谬和狭隘。<br />
<br />
&nbsp; &nbsp; 《挟尸要价》客观记录了当下某些人唯利是图、甚至趁火打劫的丑恶嘴脸，但不能因此说“荆州人冷血”。奋不顾身救人的冬泳爱好者韩德元也是荆州人，为救人英勇献身的3名大学生也可以说是荆州人（户籍在荆州）。他们的精神无处不散发着人性的光耀！<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;本人在此郑重声明，不排除启用法律手段，追究李玉泉的民事责任。<br />
