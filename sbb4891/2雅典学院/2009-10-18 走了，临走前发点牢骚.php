<br />
有关这一点，在一起阅读一则新闻之后，我们再来进行讨论。<br />
<br />
 <br />
<br />
<br />
普京警告各大国不要威胁伊朗<br />
<br />
<br />
【综合消息】 俄罗斯总理普京14日警告各大国不要威胁伊朗，并称现在谈论制裁伊朗核问题还“为时过早”。<br />
据路透社报道，普京是在美国国务卿希拉里访俄2天之后发表上述言论的。普京向记者表示:“没有必要威胁伊朗。”<br />
<br />
