<br />
<br />
●国际军事形势的异常诡秘<br />
<br />
<br />
显然，在中俄决定将一件“大家一直都在做的事情”突然“白纸黑字”的背后，是国际军事形势的异常诡秘。有关这一点，我们也将在稍后再进一步展开。<br />
<br />
