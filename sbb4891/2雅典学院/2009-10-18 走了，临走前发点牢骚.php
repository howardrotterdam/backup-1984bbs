<br />
他说:“如果现在(在核谈判前)没有做出任何举措，我们就宣布制裁措施，那么我们就不会(为核谈判)创造良好条件。这是我认为谈论制裁还为时过早的原因。”<br />
<br />
希拉里在访俄期间未能获得俄方对制裁伊朗的具体承诺，这让希拉里在美国国内备受批评，因为继美国在导弹防御问题上做出让步后，希拉里未曾从俄方取得任何回报。<br />
<br />
<br />
【时事点评】首先，请大家注意，俄罗斯总理普京警告“各大国不要威胁伊朗”，并称“现在谈论制裁伊朗核问题还为时过早”的时间是“14日”。<br />
<br />
<br />
