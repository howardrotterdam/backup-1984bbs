当然了，不排除俄罗斯此举也有意挤兑欧盟的意图：在欧亚大陆主要经济体都有心“走陆路”的新情况下，如果欧盟经济再不对俄罗斯进行投资，俄罗斯就准备对东亚的中国、甚至日本“更加开放”、从而痛失先机。<br />
<br />
而全面破坏这一“可能局面”，为发动一场表面针对中国、实际上也针对东亚、骨子里却是想楼草打兔子，将欧盟与俄罗斯也拉下水的贸易战争、甚至金融战争做最后准备，自然是希拉里腆着脸、也要如期访问“空城”，并留下“美俄战略武器谈判让步方案”、改口必须立刻制裁伊朗的原因。<br />
<br />
 <br />
<br />
●“希拉里腆着脸”之后，普京也就有了资本再去向北京提条件<br />
<br />
<br />
显然，已经在北京的普京、在看到了“希拉里腆着脸”的境头之后，也就有了资本再去向北京提条件、说要求了。客观上进，希拉里的那张脸、还是“腆”得有价值的。<br />
<br />
在东方经济评论员看来，这恐怕就是中俄实际签下的合同金额与事先宣传的有所出入的原因。大国间这种利用与被利用，早已见怪不怪！<br />
●将一件“核大国间彼此都在做的事情”正式“白纸黑字”的潜台词<br />
<br />
<br />
至于中俄间签定“战略导弹与运载火箭发射事前通报协议”，就如我们之前所说，在驻阿美军被迫向俄罗斯“借道”、从而意味着“中欧俄美”之间的角力将更集中在经济、甚至金融层面之后，不仅不意味着军事角力将不再出现，反而“有可能”以更加激烈的方式出现。<br />
<br />
<br />
显然，中俄间签定“战略导弹与运载火箭发射事前通报协议”、从而将一件“核大国间彼此都在做的事情”正式“白纸黑字”的潜台词，在美国一面摆出“准备”南亚破局（主要表现在不顾一切地强化美印关系，对中国、巴基斯坦施加压力，再用几十亿美元的援助安抚巴基斯坦，平息巴基斯坦对美印强化关系的不满）、一面摆出“准备”发动针对中国的贸易战（本月来，美国已经连续提起9起针对中国的贸易调查案）的综合背景下，其实主要就是想强调两点：<br />
<br />
第一，中俄作为上合的主导国，有足够的战略互信，特别是军事互信，因此，在形势有变的紧要情况下，不排除“中俄”中的一方，或自己，或通过上合组织，对对方在自己“重中之重”方向“可能进行的”军事行动予以理解、甚至支持；<br />
<br />
<br />
第二，我们想特别强调的是，在已经形成对“美元本位制”的“全球围攻”中，欧元与美元之间的矛盾其实已经激化，随着波兰签定了《里斯本条约》，捷克已经成为最后一个待签字国，欧盟政治统一进程也到了最后关口。<br />
<br />
&nbsp;&nbsp;<br />
<br />
●一旦如此，美国人的假戏就有可能“假戏成真”了<br />
<br />
 <br />
<br />
我们注意到，法国总统萨科齐明确警告“如果捷克继续拒绝签字，欧盟就要考虑开除其会员资格”，因此，一旦捷克顶不住欧盟核心国家的强大压力，也尾随波兰进入《里斯本条约》，美国人“对欧元让步、换欧盟经济联手敲打中国经济”的假戏就有可能“假戏成真”了，而一旦《里斯本条约》生效，也就意味着欧盟内部政治游戏规则的实质性变革，欧盟决策机制将由目前的、“每个会员都有否决权过渡为少数服从多数”，欧盟政治、军事统一进程将实质性地飞跃，欧元基础将得到空前加强，欧美矛盾将一触既发，北约组织将很难复原。<br />
<br />
 <br />
<br />
●北京介入中、东欧事务、从欧盟内部牵制欧盟核心国家的好时机<br />
<br />
<br />
在欧盟政治统一进程已经到了临门一脚，急需中国、俄罗斯“外部理解”的特殊情况下，我们认为：<br />
<br />
第一，这正是北京介入中、东欧事务、从欧盟内部牵制欧盟核心国家、尽力阻止欧美联手在经济层面攻击中国，并在科索沃问题上、地中海方向、中东和平问题上发挥更重要作用的好时机；<br />
<br />
如果从这个层面考虑问题，我们不难看出，在中国国家副主席德国之行之外，真正的重头戏在于中、东欧之行。<br />
<br />
 <br />
<br />
●时机特殊，可“有条件地”考虑俄罗斯的意见<br />
<br />
<br />
第二，我们认为，由于时机特殊，北京在“上合”组织“军事功能”的层面上，可以稍稍调整之前的那种，充分照顾欧盟的做法，从而“有条件地”考虑俄罗斯的意见，既：对欧盟、特别是对美国摆出一个“不排除为上合组织添置某种军事功能”的可能性。<br />
<br />
<br />
我们也注意到，这种可能性在目前阶段，既可视为对美国南亚政策的警告和对欧盟东欧政策的试探（对中国而言）、也可视为对欧盟东欧政策的警告和对美国中亚政策的试探（对俄罗斯而言）。<br />
<br />
<br />
●如果消息为真，则是华盛顿进一步警告北京<br />
<br />
<br />
在美国对巴基斯坦通道高度依赖的情况仍然没能根本解决，但美国已迫不急待地秘密向阿富汗大规模增兵（报道称是13000人、是否如此规模尚须证实）的情况来看，希拉里腆着脸如期访问“空城”的另一考虑，是“决心”实质性动用俄罗斯通道了，从而向北京进一步强调并警告：它准备将“南亚破局”计划从“准备执行阶段”提升至“实际执行阶段”。<br />
<br />
然而，在东方评论员看来，在阿富汗一线，不论美国是否增兵，都不足以解决阿富汗问题，唯一的好处是可以对南亚局势、施加影响。<br />
<br />
<br />
因此，在我们看来，就目前而言，华盛顿对北京的这一警告，相当程度上是为了继续鼓动印度继续挑衅中国，并直接对巴基斯坦施加军事压力，如果能导致中印之间、印巴之间“擦枪走火”，那就再好不过了。<br />
<br />
<br />
显然，为了做得更加真实，美国就必须增兵阿富汗。否则，以美国目前在阿富汗的那点兵力，既难以对印度起到鼓动作用，也难以对巴基斯坦进行恐吓、更不要说北京了。<br />
<br />
<br />
●美国秘密增兵的消息一旦为真，也就意味着美国“决心”实质性动用俄罗斯通道<br />
<br />
<br />
因此，就“俄罗斯对美国中亚政策的试探”而言，美国秘密增兵的消息一旦为真，也就意味着美国“决心”实质性动用俄罗斯通道，而一旦实质性动用，从而也就意味着“美国的东欧政策”必将向俄罗斯通道做出“令俄罗斯满意的妥协”，即便欧盟反对也不行。<br />
<br />
 <br />
<br />
●“俄罗斯对美国中亚政策的试探”是成功的！<br />
<br />
<br />
如此一来，这对欧美关系的影响巨大，从英国仅象征性、且“有条件（条件是北约其它盟国也要增兵）”增兵500，法国干脆明确表示“绝不增兵”的情况来看，对美国大规模增兵的消息，欧盟的“不高兴”是显然的。<br />
<br />
不难看出，通过该军事协议 ，“俄罗斯对美国中亚政策的试探”是成功的！<br />
<br />
 <br />
<br />
●虽不是空穴来风，但我们对增兵的规模（13000人）有所怀疑<br />
<br />
<br />
然而，东方评论员对媒体所报道的、美国秘密大规模增兵阿富汗13000人的消息所持态度是：虽不是空穴来风，但我们对增兵的规模（13000人）有所怀疑！<br />
<br />
因为，在巴基斯坦局势没有如愿之前，在寒冷的冬天即将到来之前，如此大规模的增兵，如此大规模的后勤补给“增量”，实际上等同于将驻阿美军的命运彻底交给了俄罗斯、或者上合（包括中国）。有关这方面的后续消息，我们将与大家一起关注。<br />
<br />
<br />
●“北京对欧盟东欧政策的试探”也是成功的！<br />
<br />
<br />
另外，就“北京对欧盟东欧政策的试探”而言，从中国新疆地方法院近期连续、公开宣判数名“新疆75暴力罪犯”死刑，而欧盟国家反应“总体低调”的情况来看（法国有所动作，但只是对没有欧盟法官参与表示了不满）。<br />
<br />
<br />
同样，不难看出，“北京对欧盟东欧政策的试探”也是成功的！<br />
<br />
 <br />
<br />
●“上合”在特定条件下，被“中俄”添加上“特定军事功能”的可能性也就有了很好的基础<br />
<br />
<br />
在东方评论员看来，由于“中俄”两国在两个方向，在“中俄”彼此的“重中之重”，对“欧美”的相关政策所做的战略试探都是成功的，这样，“上合”在特定条件下，被“中俄”添加上“特定军事功能”的可能性也就有了很好的基础。<br />
<br />
 <br />
<br />
●阿富汗的塔利班，似乎闻到了这一点儿！<br />
<br />
<br />
我们认为，“北京对欧盟东欧政策的试探”成功，意味着欧盟在欧盟政治统一进程临门一脚的特殊阶段，有可能“默认”上合在阿富汗方向行使“军事功能”。<br />
<br />
有意思的是，呼吁上合帮助解放阿富汗的塔利班，似乎闻到了这一点儿！<br />
<br />
 <br />
<br />
而“俄罗斯对美国中亚政策的试探”成功，则意味着美国在一旦实质性动用俄罗斯通道，也将“默认”俄罗斯可以“上合的中亚军事功能”为后盾，以各种手段巩固其在中、东欧的影响力。<br />
<br />
在这种大背景下，美国想通过“不惜代价强化美印关系，鼓动印度挑衅中国”的战略意图，很难达成目的。<br />
<br />
<br />
●那条巴控水坝的建设进程，很可能为中国提供丰富的对印斗争经验<br />
<br />
<br />
我们注意到，针对印度总理访问中国藏南地区等一系列“拿钱办事、通吃中美”的投机动作，北京的反击也显得非常巧妙：决定帮助巴基斯坦在巴控克什米尔地区修建大坝，显然，在政治上，这是个“印巴问题”，在经济上，这是个巴控克什米尔地区从经济上对印控克什米尔地区进行渗透、施加影响的问题，是个克什米尔问题；从军事上，这又是个中国与印度军事对决的问题，在印度辛格团队用高官访问等动作来“恶心中国、赚取美元、赢取选票、通吃中美”的同时，中国就用一条水坝问题巧妙地重夺了主动权：只要中印谈不好，中印领土争端就无从解决，但克什米尔问题不同，随着水坝的建设进程，巴基斯坦对印控克什米尔地区的影响力将日益增强，印控克什米尔地区的“分离势力”也将日益增强。对这一趋势，印度有什么好办法吗？<br />
<br />
事实上，在尽可能公平、合理地解决藏南的问题上，那条巴控水坝的建设进程，将为中国提供丰富的对印斗争经验，不要忘记了，在雅鲁藏布江上，中国也有条件玩一把！<br />
<br />
●需要注意两点<br />
<br />
这样，也就有了一个问题，在中国已经通过国庆展示了“可以打击地球上任何一块战略目标”的强大军事实力之后，既然美国增兵阿富汗、不惜一切强化美印关系也好，甚至不顾及“欧盟的东欧感受”也罢，只要它自己不敢亲自上阵，或者亲自打通印度-克什米尔-阿富汗通道，或者亲自出兵彻底拿下巴基斯坦通道，那么，从印度只敢在远离克什米尔的地方有所动作的情况来看，是不太容易通过印度、引发南亚军事冲突、从而实现南亚破局的企图，那么，它做的这一切又为了什么？<br />
<br />
在东方评论员看来，需要注意两点：<br />
<br />
第一点，美国在尽一切可能将“准备破局”的动作做得逼真，警告北京也好，鼓动印度、恐吓巴基斯坦，尽力拉高中国与欧盟、俄罗斯、日本等国讨价还价的门槛也罢，这都是必须的一步。<br />
<br />
因此，期间南亚的确存在有“擦枪走火”的危险性，而在某种程度上，“擦枪走火”的或然性，又极可能被某些战争贩子利用去搞“狗急跳墙”。<br />
<br />
也正因如此，中国必须做好应对一场高烈度战争的全面准备工作。这是最大限度防止借“擦枪走火”之名、行“狗急跳墙”之“实”、从而对中国战略决心做“最逼真测试”的最有效办法。<br />
<br />
第二点，需要更加警惕的是美国在“明修栈道、暗渡陈仓”，既将方方面面的政治、军事注意力都吸引在南亚方向，将几乎被中国从南亚方向推向欧美的重中之重--地中海方向（科索沃方向）的“大国利益交易平台”、再推回南亚，推回到中国的“重中之重”，从而为从经济、甚至金融层面、以“G1”身份，调动一切可以调动的力量，“全面攻击”中国做准备。<br />
<br />
<br />
如果从这个层面去看问题，那么，中国近段明显强化与俄罗斯、欧盟的战略互动，特别是经济、金融层面的互动，并默认日本的东亚共同体之提法，并“既没有正式答应、又没有正式否决”美国要求加入东亚共同体的“申请”，从而将“美国是否加入东亚共同体”、甚至是否建立东亚共同体的矛盾划为“美日矛盾”，显然也是在防着这一手。<br />
<br />
至于朝鲜再次突然发射短程导弹，在之前的点评中，我们已经说过：这既是对仍然有意紧跟“美国东北亚政策”的韩国政府的一种警告，更是将“南亚的军事形势”与“东北亚的经济形势”做“有效鉴别”的一种方式，也就是说：朝鲜只是试射可以攻击韩国、及驻韩美军的短程导弹，明显不是“为呼应巴基斯坦呼救、准备在东亚破局”的玩法，因此，目前还用不着去打日本核武装这张安全牌，冲着最受不了紧张气氛的韩国、在经济层面提醒韩国、或者日本“小心一点儿”也就足够了。<br />
<br />
因此，我们的感觉是：北京显然注意到了“第一点”，但更加警惕“第二点”；</div>

							
							
							
							
