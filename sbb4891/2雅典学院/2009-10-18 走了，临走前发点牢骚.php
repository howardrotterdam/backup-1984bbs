在结束了东亚部分之后，下面，东方时事评论员将与东方经济评论员一道，开始今天的南亚部分。<br />
我们知道，在俄罗斯总理普京此次来访过程中，中俄签定的总计达35亿美元的一蓝子协议，其中主要包括有这样几样：<br />
