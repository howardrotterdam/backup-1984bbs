第三，能源方面的一系列合作；<br />
<br />
上面是商业方面的合作协议。在安全层面，值得关注的是中俄军方有关负责人13日在人民大会堂签署了两国政府关于相互通报发射弹道导弹和航天运载火箭的协定。<br />
