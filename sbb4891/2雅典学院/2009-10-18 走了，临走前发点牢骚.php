<br />
这就是说，仅就时间的巧合而言，作为俄罗斯最有实权的政治领袖，普京对以美国国务聊的身份首次访俄的希拉里玩了出“避免与其相难见”的“空城记”。<br />
<br />
●希拉里访俄对中俄之间的“这一轮合作”、还是起到了某些干扰作用的<br />
<br />
在这个基础上，结合中俄签了40多亿而不是55亿合同的事实，我们再来回顾希拉里的、被媒体形容为毫无收获的访俄行程，也就不难明白，将希拉里访俄说成“空手而归”也不尽准确：这就是说，希拉里访俄对中俄之间的“这一轮合作”、还是起到了某些干扰作用的。<br />
