中央社今天引述香港媒体报道说，熟悉中国国务院内情人士向香港媒体透露，总理温家宝虽然民望居高不下，但权力却已遭侵蚀。有消息说，“温总的话，在国务院内已经没人听了”，在第二任期还有3年就出现提前“跛脚”情况。香港明报今天也同时刊出“温总声望虽隆，官场暗涌横流”的文章。<br />
