<br />
报道因此认为，温家宝名义上是总管，却并不直接具体负责任何领域，即使是国家能源委和国防动员会这两个跨部会超级机构，也都由李克强兼任副主任。温家宝的地位虽然还没有危险，但他的权力被大幅度分解是不争的事实。<br />
