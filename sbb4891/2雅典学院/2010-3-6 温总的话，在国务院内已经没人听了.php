																											<div id="postmessage_506308" class="t_msgfont">同学，你知道影帝在国家机构中的法定权责是什么吗？<br />
<br />
--------------<br />
【国务院的领导体制国务院的领导体制 】<br />
<br />
理负责制即行政首长负责制，是指【国务院总理对其主持的国务院工作有完全的决定权并承担全部责任】。具体内容为：&nbsp;&nbsp;<br />
<br />
1、由总理提名组织国务院。&nbsp;&nbsp;<br />
2、总理领导国务院工作，副总理、国务委员协助总理工作，国务院其他组成人员都是在总理领导下工作，向总理负责；&nbsp;&nbsp;<br />
3、【总理主持召开常务会议和全体会议，总理拥有最后决定权，并对决定的后果承担全部责任】；&nbsp;&nbsp;<br />
4、国务院发布的决定、命令和行政法规，向全国人大及其常委会提出的议案，任免国务院有关人员的决定，都得由总理签署。<br />
&nbsp;&nbsp;<br />
国务院实行总理负责制是由国务院的性质和任务决定的。国务院的性质是行政机关，任务是执行国家权力机关的决定。&nbsp;&nbsp;<br />
