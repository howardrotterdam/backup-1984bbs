																											<div id="postmessage_833251" class="t_msgfont">《求是》：划清中国特色民主同西方民主界限<br />
<a href="http://news.qq.com/a/20100916/002023.htm" target="_blank">http://news.qq.com/a/20100916/002023.htm</a><br />
党的十七届四中全会通过的《中共中央关于加强和改进新形势下党的建设若干重大问题的决定》指出：“加强党的意识形态工作和思想政治工作，引导党员、干部增强政治敏锐性和政治鉴别力，筑牢思想防线，自觉划清马克思主义同反马克思主义的界限，社会主义公有制为主体、多种所有制经济共同发展的基本经济制度同私有化和单一公有制的界限，中国特色社会主义民主同西方资本主义民主的界限，社会主义思想文化同封建主义、资本主义腐朽思想文化的界限，坚决抵制各种错误思想影响，始终保持立场坚定、头脑清醒。”<br />
　　这里提出要划清的四个界限，都是关系中国特色社会主义道路的重大问题。<br />
　　一、关于划清“四个重大界限”的理论与实践意义<br />
　　改革开放以来中国的发展进步举世公认，改革发展的成果惠及国人。最突出的是：中国经济30年来以年均增长9.8%的速度持续快速发展，是同期世界经济年均增长率的三倍多，经济总量跃居世界前列，人民生活实现了从温饱不足到总体小康的历史性跨越；伴随着经济生活的深刻变革，中国社会焕发出蓬勃旺盛的活力，人民享有越来越广泛的自由和权利，勤劳智慧的中国人民迸发出巨大创造能量；在经济体制深刻变革、社会结构深刻变动、利益关系深刻调整、思想观念深刻变化的过程中，我国社会大局始终稳定，长期保持了政通人和、安定团结的政治局面。这一切，对于我们这样一个有着13亿人口的发展中大国来说，都是了不起的成就，是当之无愧的“中国奇迹”。<br />
　　对改革开放的伟大历史进程和经验，我们党在不断地进行总结。党的十七大指出：“改革开放以来我们取得一切成绩和进步的根本原因，归结起来就是：开辟了中国特色社会主义道路，形成了中国特色社会主义理论体系。”包括邓小平理论、“三个代表”重要思想、科学发展观等重大战略思想在内的中国特色社会主义理论体系，是中国特色社会主义道路的理论表现，也是我们理解和说明中国发展道路的最权威的理论。<br />
　　但是，对于中国这30多年走过的路，也有人作出别样的解读和评说，并且在竭力同我们党争夺话语权和影响力。有人以“反思改革”为名，得出中国的改革走入歧途、已经背离社会主义道路的结论，实际上就是否定改革，主张回过头去走封闭僵化的老路；有人把西方的自由、民主、人权说成是“普世价值”，鼓吹中国只有接受这些“普世价值”、照搬西方制度模式才有光明前途，实际上就是主张走改旗易帜的邪路。<br />
　　对于中国的发展模式，国际上也议论纷纭。西方一些势力虽然不能不承认中国的经济成就，但出于其政治立场和意识形态偏见，从来没有也不可能认可中国的政治制度，反而认为中国的成功会威胁到他们的制度模式和价值观。<br />
　　政治上的坚定来自理论上的清醒。在关系党和国家发展道路、未来走向的重大问题上，我们必须旗帜鲜明。近年来特别是去年，中央主要媒体推出了大量文章，深入回答“六个为什么”；这次中央提出划清“四个重大界限”，是回答“六个为什么”的继续和深化，而且使问题更集中。可以说，划清“四个重大界限”，有助于把握中国特色社会主义道路的“基本面”，有助于回答国际国内对改革开放以来“中国模式”的主要关切，有助于抵制错误思想、澄清模糊认识、坚定理想信念，因而具有重大的理论与实践意义。<br />
　　二、关于划清马克思主义同反马克思主义的界限<br />
　　马克思主义是我们立党立国的根本指导思想，是社会主义意识形态的旗帜和灵魂。因此，划清马克思主义同反马克思主义的界限，对于正确认识中国特色社会主义道路，就有第一位的意义。<br />
　　什么是马克思主义？首先，马克思主义是科学。马克思创立的辩证唯物主义和历史唯物主义世界观和方法论，照亮了人们对自然界、人类社会和人类思维发展普遍规律的探索之路。尽管马克思逝世一百多年来世界发生了许多重大变化，但是所有这些变化都没有脱离马克思主义揭示的人类社会发展规律，都可以用马克思主义基本原理加以解释和说明。其次，马克思主义是工人阶级和劳动人民的思想武器。马克思对资本主义经济运动作了最彻底、最深刻、最缜密的研究，揭示了资本主义灭亡的历史必然性和工人阶级的历史使命。马克思主义最鲜明的政治立场，是为实现工人阶级和劳动人民的根本利益而奋斗；马克思主义关于消灭一切不公正社会制度、实现全人类解放和人的自由全面发展的学说，代表了整个进步人类的理想和信念。其三，马克思主义是开放的与时俱进的理论体系。马克思和恩格斯都强调，他们的理论是发展着的理论，而不是必须背得烂熟并机械地加以重复的教条。马克思主义并没有终结真理，而只是开辟了认识真理的道路。一切从实际出发，理论联系实际，实事求是，在实践中检验真理和发展真理，是马克思主义最重要的理论品格。<br />
　　中国共产党是非常重视理论指导的政党。在把马克思主义基本原理同中国具体实际相结合的过程中，我们党创立了毛泽东思想和包括邓小平理论、“三个代表”重要思想以及科学发展观等重大战略思想在内的中国特色社会主义理论体系。这些理论成果都是中国化的马克思主义，都是我们党对马克思主义理论宝库的重大贡献。正是由于党在指导思想和基本理论上的与时俱进，我们党的实践才能够体现时代性、把握规律性、富于创造性。在当代中国，坚持中国特色社会主义理论体系，就是真正坚持马克思主义。<br />
　　什么是反马克思主义？从历史和现实看，反马克思主义的东西集中在两个方面：其一，从右的方面否认马克思主义的科学性，反对马克思主义的政治立场、思想体系和基本原理，反对马克思主义的指导地位和作用。现实中，有人宣扬马克思主义“过时论”、“无用论”；有人主张用西方的所谓“普世价值”取代马克思主义的指导地位。其实质和要害，就是主张全盘西化，主张用西方资本主义制度模式取代中国共产党的领导和社会主义国家政权。其二，从“左”的方面打着马克思主义的旗号反对马克思主义的本质，根本否定我国的改革开放，否定中国特色社会主义。实践证明，把马克思主义当成僵死的教条，拿书本教条来裁判党和人民生机勃勃的历史创造活动，不但不符合马克思主义，恰恰是马克思主义的大敌，是党和人民事业的大敌。<br />
　　划清马克思主义同反马克思主义的界限，有两点特别重要：一是政治立场。马克思主义是世界观与价值观的统一。作为世界观，它是严谨的科学，是对客观世界、客观事物、客观规律的科学反映；作为价值观，它代表的是工人阶级和劳动人民亦即最广大人民的根本利益，反映的是最广大人民对共同富裕、公平正义的价值追求。是否始终站在最广大人民的立场上，是马克思主义与反马克思主义的分水岭和试金石。二是思想路线。只有活的理论才管用，僵死的理论只会害死人。解放思想、实事求是、与时俱进是马克思主义活的灵魂和思想路线，任何时候都要坚持。明明在实践中行不通的理论还要死抱着不放，那是病态和愚蠢，只会自取灭亡。<br />
　　怎样对待非马克思主义？在当今思想界，除了反马克思主义的东西，还有大量非马克思主义的思潮和学说。在国内，社会思想意识多元、多样、多变的趋势日益发展，思想理论领域学派纷呈、热点频现；国际上，既有形形色色的左翼思潮，也有日趋活跃的绿色环保思潮等；宗教在世界范围都有广泛影响，其“有神论”的基本思想同马克思主义的“无神论”显然是对立的。如果我们把这些不符合甚至违背马克思主义的东西统统归于反马克思主义，那只会四面树敌、孤立自己。一部马克思主义发展史，就是随着时代、实践和科学的发展，吸收人类创造的一切文明成果，在同各种思想理论的相互激荡和斗争中，不断丰富、完善和创新的历史。对反马克思主义的东西要斗争；对非马克思主义的思想，既要斗争，也要尊重、包容、借鉴、吸收。既尊重差异、包容多样，又有力抵制各种错误和腐朽思想的影响，是坚持和发展马克思主义的题中应有之义。<br />
　　任何国家和社会都有占统治地位的意识形态，意识形态领域的主导思想从来都是一元的，不能多元化。社会思想观念越是多样化，就越是需要坚持和巩固马克思主义的指导地位。如果动摇了马克思主义的指导地位，就会动摇中国特色社会主义的理论根基，动摇全党全国人民团结统一的思想基础。只有坚持马克思主义指导思想，才能有效引领和整合社会思潮，在尊重差异中扩大社会认同，在包容多样中形成思想共识，从而凝聚起建设国家的伟大力量。<br />
　　三、关于划清社会主义公有制为主体、多种所有制经济共同发展的基本经济制度同私有化和单一公有制的界限<br />
　　划清这个界限，是正确理解“中国奇迹”的一把钥匙。<br />
　　第一，中国经济改革的实质，就是要在社会主义条件下发展市场经济，或者说是要把坚持社会主义基本制度同发展市场经济结合起来。邓小平同志讲：“社会主义的本质，是解放生产力，发展生产力，消灭剥削，消除两极分化，最终达到共同富裕。”这里，消灭剥削、消除两极分化、最终达到共同富裕，都是社会主义的价值诉求、价值目标，而解放生产力、发展生产力，则是实现价值目标必须遵循的客观规律。我们过去的错误，不在价值取向错了，而在违背了客观规律，结果事与愿违。我们党把经济体制改革的目标确定为建立社会主义市场经济体制，以利于解放和发展生产力，这解决了关系整个社会主义现代化建设全局的一个重大问题。<br />
　　商品经济的充分发展，是社会经济发展不可逾越的阶段。市场经济是商品经济条件下社会经济资源的一种配置方式、一种经济运行机制，是人类创造的重要文明成果，但却是在资本主义条件下创造的。在社会主义条件下发展市场经济，是我们党和人民的伟大创举。社会主义市场经济的蓬勃发展，给我国经济社会注入了以往一切时代都不可比拟的生机和活力。改革开放以来我国经济社会的一切发展进步，都是以社会主义市场经济的发展为基础的。把市场经济成功“嫁接”在社会主义制度上，是我们党对马克思主义和社会主义的历史性贡献。<br />
　　第二，公有制为主体、多种所有制经济共同发展的基本经济制度，是社会主义市场经济的基础。改革开放之前我国“一大二公”、近乎单一公有制的所有制形式，脱离了生产力发展状况，导致生产效率低下、经济发展停滞、人民生活困难。改革开放以来，我们党认真总结经验教训，在新的实践基础上不断深化对所有制问题的认识。确立公有制为主体、多种所有制经济共同发展的基本经济制度，正是基于三个认识：第一，我国是社会主义国家，必须坚持公有制作为社会主义经济制度的基础；第二，我国处在社会主义初级阶段，需要在公有制为主体的条件下发展多种所有制经济；第三，一切符合“三个有利于”的所有制形式都可以而且应该用来为社会主义服务。<br />
　　多种所有制经济共同发展，是市场经济的必然要求。市场经济是一种交换的经济，要求有产权清晰的多元化的市场主体，要求各市场主体自主决策、自主经营，要求市场主体之间形成公平的竞争关系。即便同是公有制经济，也要求建立产权清晰、权责明确、政企分开、管理科学的现代企业制度，使企业成为适应市场的法人实体和竞争主体。实践证明，实行公有制为主体、多种所有制经济共同发展的基本经济制度，逐步消除了所有制结构不合理对生产力的羁绊，形成了公有制实现形式多样化和多种经济成分共同发展的局面，促进了社会生产力的大解放、大发展。<br />
　　我们党在确定改革目标时就明确强调，建立社会主义市场经济体制，就是要使市场在国家宏观调控下对资源配置起基础性作用。改革开放以来，我国经济在持续快速增长的同时，有效抑制了市场经济的周期性起落，总体上保持了平稳发展的态势，充分显示了社会主义国家宏观调控的巨大威力。没有公有制经济的主体地位和主导作用，宏观调控就不可能如此有效。宏观调控与市场机制的有机结合，既有利于抑制市场经济的自身弱点和消极方面，又可以让一切劳动、知识、技术、管理和资本的活力竞相迸发，让一切创造社会财富的源泉充分涌流。<br />
　　第三，廓清在所有制问题上的种种迷雾，毫不动摇地坚持社会主义初级阶段的基本经济制度。在确立社会主义初级阶段基本经济制度的过程中，一直伴随着思想交锋。反对的声音来自“左”、右两方面。右的观点视公有制为万恶之源，把私有化说成包治百病的良药，极力主张全盘私有化；“左”的观点则认为发展非公有制经济就是搞私有化，就是发展资本主义。<br />
　　公有制是社会主义经济制度的基础，没有公有制就没有社会主义。改革开放以来的实践表明，坚持公有制为主体，没有妨害经济发展，恰恰相反，它是中国经济持续快速发展的重要制度保障。土地是最基本的生产资料。这30多年来中国经济的高速成长，是以工业化、城市化为主要推动力的，而中国的城乡土地公有制为工业化、城市化的迅速推进提供了有规划、高效率、低成本的土地资源支撑，同时又避免了世界上一些实行土地私有制的国家在工业化、城市化进程中出现的城市贫民窟等现象。虽然在土地征用、房屋拆迁中发生了一些问题，但那并不是主流。事实的主要方面是：土地公有制是中国能够创造经济奇迹的最大“奥秘”之一。<br />
　　苏联解体后俄罗斯以“休克疗法”搞私有化运动，结果导致经济急剧衰退，贫富两极分化，财富集中于极少数寡头。在拉美地区，实行西方兜售的新自由主义经济政策、搞私有化越坚决的国家，危机就越严重。美国华尔街的金融巨头是这次国际金融危机的始作俑者，它们的高管在正常年份大把捞钱、薪酬高得吓人，而危机爆发后却要由美国政府拿纳税人的钱为其注资救助、实行部分国有化，甚至一边拿政府救助一边还要发放高额红利，激起公愤。问题在于这些大银行、大公司掌握着美国经济的核心和命脉，大到不能倒闭。这难道不恰好证明中国由国有经济控制国民经济命脉的体制的合理性吗？<br />
　　对我国改革发展过程中出现的收入差距拉大、城乡区域发展不平衡、经济社会发展不协调、资源环境压力加大等问题究竟应该怎么看？从实际出发看问题，深层原因在于：我国人口多、底子薄、发展不平衡、生产力不发达，正处于并将长期处于社会主义初级阶段，这个基本国情是我们为什么必须实行现在这样的政策而不能实行别样的政策的根本依据；我国面临经济全球化深入发展和世界经济结构调整造成的重要发展机遇，机遇稍纵即逝、时不我待，但在抢抓机遇、加快发展过程中难免会发生偏重于经济增长、经济效率而对社会发展、社会公平、资源节约、环境保护等重视不够、兼顾不够的问题；市场机制主要解决的是经济效率问题，在许多领域市场是失灵的，我国在发展过程中出现的许多问题，在一定程度上正是市场缺陷的产物，也表明我们对在社会主义条件下发展市场经济的经验不足；任何国家要发展都必须对外开放，封闭只能窒息自己的生机，但是当今世界经济体系是资本主义主导的经济体系，我们在对外开放、利用外资、发展外贸的同时难免要付出代价、要承受不合理国际经济秩序带来的压力。我们党提出科学发展观，正是为了解决改革发展进程中出现的这些问题。<br />
　　总之，我们是在既定的历史条件和内外环境下建设社会主义的，这种历史条件和内外环境不由我们选择，也无法超越。我们只能从实际出发，务实地解决改革、发展、稳定的问题，任何因循守旧、拘泥于书本教条的想法和做法都只会葬送社会主义、葬送党和国家的前途。只有这样看问题，我们才能在理论与实践的结合上自觉划清公有制为主体、多种所有制经济共同发展同私有化和单一公有制的界限，毫不动摇地坚持社会主义初级阶段的基本经济制度。<br />
