四、关于划清中国特色社会主义民主同西方资本主义民主的界限<br />
　　划清这个界限，是正确理解“中国奇迹”的另一把钥匙。<br />
　　第一，发展是中国的首要任务，稳定是中国的核心利益。近代史上中国积贫积弱，备受列强欺凌，实现国家富强、民族复兴是全民族最强烈的愿望；中国历史上长期动荡、战乱频仍，人民深受其害，能否实现社会团结稳定和国家长治久安，是衡量政治制度是否适用的最重要标准。近代以来中国真正在社会政治稳定的条件下发展经济，只有新中国成立后特别是改革开放以来这几十年时间。离开共产党的领导，搞西式民主，中国肯定会来一次大折腾，折腾出一个政党林立、纷争四起、国无宁日、动乱不已的乱世，折腾成一个四分五裂、虚弱不堪的西方附庸国。事实证明，我国现行的政治制度保证了国家的统一、政权的稳定、人民的团结，保证中国能够沿着正确道路长期稳定发展。<br />
　　第二，共产党的坚强领导是中国最大的政治优势。我们党从来就以实现和发展人民民主为己任。党执政后领导人民进行了广泛的民主实践，在根本改变劳动人民的社会地位和历史命运、实现社会平等和人民当家作主等方面取得了旧中国不可比拟的巨大进步。党在民主问题上犯过错误、走过弯路。但从本质上看，是党和党的领导人在群众中享有崇高威望，党内产生骄傲自满、个人崇拜、头脑不清醒导致的，也与中国历史上封建专制主义传统的影响，与我们党在长期战争环境中形成的领导方式，与共产国际和苏联模式影响，与应对新中国成立后国际敌对势力的包围，与经验不足等因素有关，这与反民主的独裁、专制的性质根本不同。为国为民，是我们党不变的灵魂，这使党具有强大的自我纠错、自我更新能力，能够郑重对待自己的失误，总结经验教训，勇于改革创新。改革开放以来，我们不仅创造了经济高速成长、社会充满活力、政治安定团结的成就，而且战胜了一个又一个突如其来的重特大自然灾害和传染病疫情，从容应对了一系列关系我国主权和安全的重大突发事件，经受住了一次又一次国际政治、经济风浪的重大考验。香港、澳门回归，“神舟”、“嫦娥”飞天，北京奥运会的巨大成功，抗震救灾和应对国际金融危机冲击的出色表现，都是在党的领导下取得的；市场基础上强有力的宏观调控，集中力量办大事的举国体制，全国一盘棋的动员组织能力，“一方有难、八方支援”的互助协作机制，这些在国际比较中显示出巨大优势的“中国特色”，离开党的领导也是不可想象的。一切不抱偏见的人们都应该承认：共产党的坚强领导，是创造“中国奇迹”的政治保证，也是别的国家比不了、学不来的制度因素。<br />
　　第三，中国经济持续快速发展的事实证明，我国的政治体制是基本适应经济发展要求的。西方看中国的一个流行观点是：经济上是好的，政治上是坏的；经济上在改革，政治上不改革；经济上在搞自由市场经济，政治上还是极权专制。这是一种根本站不住脚的观点。经济决定政治，政治是经济的集中体现；政治制度、政治体制必须适应经济发展的需要，如果不适应并阻碍了经济发展，它就必然要被改变。事实上，中国的改革开放就是一项政治决定，它改变了过去严重束缚经济发展的旧体制，使中国走上了一条新路。30多年来中国经济的高速成长并不是孤军突进的，而是伴随着政治、文化、社会等方面广泛深刻的变革。中国经济的巨大成功，反过来证明了中国政治上的成功。在当今世界，我们看不到还有哪个与中国有可比性的国家比中国发展得更好、更成功；相反，我们看到了很多可以从反面或者侧面证明中国的路走对了的事实。<br />
　　第四，民主法治、自由平等、公平正义是社会主义的本质要求，要在实践中不断探索、逐步实现。民主从来都是具体的、历史的、不断发展的，不同的国家、不同的历史发展阶段，其实现形式和途径各不相同，没有统一的模式。在当代中国，公有制为主体的基本经济制度、人民代表大会制度、中国共产党领导的多党合作和政治协商制度、民族区域自治制度以及基层群众自治制度，为实现社会主义民主提供了经济基础和政治保障。党和国家政治生活中的民主集中制原则、各级领导层实行的集体领导制度、党和政府工作中的群众路线和民主作风、党同人民群众的密切联系等，是国家生活中切实管用的民主机制、民主因素，是中国式民主的特点和优势。我们承认由于历史文化传统和现实条件的制约，中国在实现人民民主方面还有许多不尽如人意的地方，还有很长的路要走。我们将顺应经济社会的发展变化，适应人民政治参与积极性的不断提高，坚持党的领导、人民当家作主、依法治国有机统一，不断深化政治体制改革，不断推进中国特色社会主义民主政治的发展。我们需要借鉴人类政治文明有益成果，借鉴世界各国包括西方发达国家政治发展的有益经验，但不会照搬西方政治制度模式，不会吞下损害自己利益的苦果。<br />
　　第五，西方资本主义民主没有“普世性”，照搬别国模式从来不能成功。现代西方民主制度是经过几百年的发展才达到今天这样的水平，而且已经是弊端丛生。一是金钱操控。西方民主早已成为“富人的游戏”、“钱袋的民主”和资本玩弄民意的过程，竞选活动拼的就是金钱。西方民主游戏中的权钱交易固然与当事人个人操守有关，但根本原因还在制度层面：烧钱的选举必然产生捞钱的腐败。二是形式至上。现代西方政治已使民主形式化、工具化，造成民主的异化。选举是西方民主最主要的形式，几年一次的选举搞得挺热闹，但选举一过，民主就“沉睡”了，民众对政府日常施政的影响力十分有限。西方向世界输出民主，实际上就是输出选举，甚至不惜用飞机大炮、狂轰滥炸把西式选举强加给别国，以为只要搞了选举就是民主国家了。三是僵化停滞。西方民主已成固定模式，逐渐丧失活力，任何改变和创新都十分艰难。民主的僵化常常导致决策上的僵局，反对党“为反对而反对”，致使一些重大社会问题长期得不到解决；僵化和停滞还造成优秀政治人才严重短缺，不少西方国家为选不出合适的领导人而苦恼。放眼当今世界，与西方历史文化传统不同而照搬西方民主制度的国家，没有一个是真正成功的。<br />
　　胡锦涛总书记指出：“衡量一个政治制度是不是民主的，关键要看最广大人民的意愿是否得到了充分反映，最广大人民当家作主的权利是否得到了充分实现，最广大人民的合法权益是否得到了充分保障。”这就是我们的民主观。中国共产党是有伟大创造力的党。党一定能够领导人民探索建立起一套不同于西方、符合中国实际、具有自己特点的民主政治制度体系，我们应该对此充满信心。<br />
