五、关于划清社会主义思想文化同封建主义、资本主义腐朽思想文化的界限<br />
　　当今时代，文化越来越成为民族凝聚力和创造力的重要源泉，意识形态等软实力因素在国际竞争和斗争中的作用越来越突出。划清社会主义思想文化同封建主义、资本主义腐朽思想文化的界限，对于我们正确认识和自觉坚持中国特色社会主义道路，至关重要。<br />
　　第一，社会主义核心价值体系是社会主义意识形态的本质体现，是我国思想文化领域的主旋律。在改革开放的历史进程中，一方面，马克思主义在创新中不断发展，党的理论创新成果日益深入人心，人们对中国特色社会主义道路的认同度不断提高，爱国主义和改革创新成为思想精神领域最强劲的主流；另一方面，在各种因素影响下，封建主义残余思想意识仍广泛存在，国外资本主义腐朽思想也乘机而入，各种思想文化相互交融、激荡十分突出。在思想大活跃、观念大碰撞、文化大交融的新形势下，建设社会主义核心价值体系，具有极强的现实针对性和重大的理论与实践意义。<br />
　　马克思主义指导思想、中国特色社会主义共同理想、以爱国主义为核心的民族精神和以改革创新为核心的时代精神、社会主义荣辱观，构成社会主义核心价值体系的基本内容。这四个方面的基本内容，都是社会主义意识形态最本质、最重要的部分。在这个价值体系中，马克思主义指导思想是灵魂，提供的是科学的世界观和方法论，是建设社会主义的理论基础和行动指南；中国特色社会主义共同理想是主题，鼓舞和激励着全国各族人民在中国共产党领导下，走中国特色社会主义道路，为实现中华民族的伟大复兴而奋斗；民族精神和时代精神是精髓，深深熔铸在我们民族的生命力、创造力和凝聚力之中，为民族复兴提供着不竭动力；社会主义荣辱观是基础，为在社会生活中确定价值取向、作出道德判断提供基本准则，发挥着引领风尚的作用。这四个方面的基本内容，相互联系、相互贯通、有机统一，共同构成一个完整的价值体系。这个价值体系应该是相对稳定的，要长期起作用；又不是一成不变的，必定随着社会主义的发展、时代的发展和人们社会实践的发展而不断发展。这个价值体系不应该是封闭的，而应该是开放的，必须要吸收人类创造的一切先进、有益的思想文化成果，不断丰富和完善自己。更重要的是，这个价值体系需要在全社会宣传推广，为广大社会成员所感知、所认同、所接受、所掌握，真正成为社会精神生活的主旋律，成为社会发展进步的生命线。<br />
　　第二，封建主义思想文化的残余影响仍然广泛存在，肃清这种残余影响仍然是一项长期艰巨的任务。邓小平同志曾经说过，旧中国留给我们的，封建专制传统比较多，民主法制传统很少，解放以后，我们也没有自觉地、系统地建立保障人民民主权利的各项制度，法制很不完备，也很不受重视。他深刻指出，党和国家领导制度、干部制度中存在的官僚主义现象、权力过分集中的现象、家长制现象、干部领导职务终身制现象和形形色色的特权现象，多少都带有封建主义色彩。他指出，封建主义残余影响还表现在社会关系中残存的宗法观念、等级观念，上下级关系和干群关系中在身份上的某些不平等现象，公民权利义务观念薄弱，经济领域的官商作风、本位主义，文化领域的专制主义作风，对科学教育的不重视，对外关系中的闭关锁国、夜郎自大，等等。他强调，对待继续肃清思想政治方面封建主义残余影响的任务，要有实事求是的科学态度。首先，要划清社会主义同封建主义的界限，决不允许借反封建主义之名来反社会主义，也决不允许用假社会主义来搞封建主义；其次，也要划清文化遗产中民主性精华同封建性糟粕的界限；还要划清封建主义遗毒同我们工作中由于缺乏经验而产生的某些不科学的办法、不健全的制度的界限。邓小平同志的这些分析和论述，对于我们今天划清社会主义思想文化同封建主义腐朽思想文化的界限，仍然有着重要的指导意义。<br />
　　改革开放以来，随着我国经济社会的发展进步，封建主义思想文化残余受到进一步的涤荡。特别是以胡锦涛同志为总书记的党中央提出科学发展观以来，从中央到地方，从政府施政到百姓生活，到处都在讲以人为本，都在讲尊重人民主体地位、保障人民各项权益、促进人的全面发展，都在讲发展为了人民、发展依靠人民、发展成果由人民共享。这种“以人为本、人民至上”的理念，是反对封建主义思想文化最重要的思想武器，也是指引中国社会未来发展进步的重要战略思想，其意义是极其重大而深远的。<br />
　　第三，世界范围内围绕发展模式和价值观的斗争日益凸显，资本主义思想文化的影响正日益成为我们面对的主要挑战。资产阶级在反对封建等级制度的斗争中，提出了自由、平等、民主、人权等口号，以此为思想武器赢得了人民的支持并取得胜利，推动了社会的进步。但是，随着资本主义经济关系的发展，资本主义社会不可避免地形成了以拜金主义、享乐主义、极端个人主义为实质内容的核心价值观。在西方社会现实中，金钱、财富决定一切，决定你的自由度，决定你在民主游戏中地位，决定你享有的人权水平；如果没有金钱至上的价值观，没有对享乐、消费的无限贪欲，没有在财富欲望驱动下的“个人奋斗”，资本主义就不可能得到发展。马克思主义经典作家对资本主义社会金钱至上的本质，曾经给予大量深刻的揭露和批判。这场发端于美国的国际金融危机，再次暴露了那些作为资本人格化的华尔街精英们追逐金钱、贪婪无度的本性。无数的事实表明：西方公开标榜的自由、民主、人权，早已不再是资本主义社会的核心价值，而只是美妙动人的广告语和贴在门上的标签。拜金主义、享乐主义、极端个人主义才是资本主义社会的精神内核，而资本主义制度下的自由、民主、人权不过是包裹在这种内核外面，并为其提供条件和保障的抽象话语、法律条文。<br />
　　冷战结束以后，西方发达国家一直利用其经济科技强势地位，对发展中国家进行思想文化渗透，推销西方价值观念和制度模式，企图建立西方意识形态和社会制度一统天下的国际格局。但是，接受西方价值观念和社会制度的一些发展中国家政局动荡、经济萧条的事实，使西方社会制度和发展模式广遭诟病。特别是这场国际金融危机的爆发，进一步引发了国际上对西方制度模式的反思。但是，由于西方垄断国际舆论话语权，长期宣传西方意识形态，使国际社会形成顽固的意识形态偏见。在我国社会全方位对外开放和西方主导国际舆论的情况下，西方资本主义思想文化对国内的影响正日趋扩大。<br />
　　我们同各种敌对势力在意识形态领域的斗争，本质上是社会主义价值体系同资本主义价值体系的较量。社会主义核心价值体系，是社会主义制度的内在精神之魂。只有努力构建具有广泛感召力的社会主义核心价值体系，用以引领和整合多样化的思想观念和社会思潮，才能在尊重差异、包容多样的基础上保持全社会共同的理想信念和道德规范，形成全民族奋发向上的精神力量和团结和睦的精神纽带，打牢全党全国各族人民团结奋斗的共同思想基础，使13亿人民团结起来，万众一心推进中国特色社会主义伟大事业。</div>

							
							
							
							
