　　民主作为人类社会一种文明的政治制度，百余年来一直是中国人民孜孜追求的理想目标。但对于如何实现民主、实现什么样的民主，始终存在着两种根本不同的道路和模式：一种是把马克思主义与中国实际相结合，走自己的路，建设中国特色社会主义民主；另一种是走全盘西化道路，把西方资本主义民主照搬到中国。中国共产党把马克思列宁主义与中国革命的具体实践相结合，领导中国人民推翻了帝国主义、封建主义和官僚资本主义的统治，创建了工人阶级领导的工农联盟为基础的人民民主专政的国家，在中国第一次实现了真正的人民民主，并从中国的具体国情出发，创立了人民代表大会制度、共产党领导的多党合作和政治协商制度、民族区域自治制度、基层群众自治制度等基本政治制度和一系列具体政治制度，找到了中国特色社会主义政治发展道路，形成了与西方资本主义民主根本不同的政治模式。<br />
<br />
　　中国特色社会主义民主与西方资本主义民主相比具有巨大的优越性，归结起来主要有两条：第一，中国特色社会主义民主是人类历史上先进的社会主义性质的民主，它把过去只有少数剥削者才能享受的民主变为大多数人都可以享受的人民民主，使广大人民群众真正成为国家和社会的主人，拥有广泛而真实的民主权利；第二，中国特色社会主义民主是中国人民创造的适合中国国情的民主。世界上并不存在惟一的、普遍适用的和绝对的民主模式，关键是要看它是否符合本国人民群众的要求，是否符合本国的实际情况。中国特色社会主义民主是共产党领导中国人民长期奋斗和艰苦探索得来的，既遵循民主发展的一般规律，又具有鲜明的中国特色，已经被实践证明是完全符合中国实际和中国人民愿望的政治制度。它实现了人民当家作主，有利于国家政治稳定，能够调动起广大人民建设美好幸福生活的积极性和创造性，为我国经济社会发展提供了强大动力和坚强政治保证。现在越来越多的人都在关注中国取得巨大成功的奥秘，研究中国发展模式的内在优势，其中的重要内容之一，就是中国特色社会主义民主与西方资本主义民主有什么不同、有什么优势。<br />
<br />
　　二、真正的人民民主，还是实质上的金元民主？<br />
<br />
　　民主是伴随着阶级和国家而产生的，具有鲜明的阶级性。在奴隶社会和封建社会，普遍实行的是专制政体，即使是极少数实行民主政体的国家，也只有奴隶主阶级和封建主阶级才享有民主权利。资本主义社会打破了封建等级制和特权思想，根据所谓“天赋人权”理论，在法律上承认所有公民都享有平等的民主权利，这无疑是历史的重大进步。然而，资产阶级法权有其不可克服的局限性，这就是它以同一的尺度去衡量实际情况各不相同的人，于是就产生了形式上平等而实际上不平等的结果。从形式上看，所有的公民都有选举权和被选举权，都可以参加国家事务管理，但由于资本主义社会是以生产资料私有制为基础的，存在着严重的阶级对立和贫富两极分化，这就使每个人实际享有的民主权利出现了天壤之别。马克思在谈到人的发展历史时，称资本主义社会人的独立性是“以物的依赖性为基础的人的独立性”。从表面上看，每个人都是独立、自由、平等的，但由于人与物的关系在这里是颠倒的，人是完全依赖于物的，所以在资本主义社会，金钱才是决定一切的，金钱能够左右政治，左右人们的民主权利。各个资产阶级垄断集团为了掌控国家政权，利用金钱收买选票，操纵媒体，推介自己的候选人。整个竞选活动实际上是资本集团的财力比拼，是资本操纵下的政治游戏。选举中当选的议员和政府官员大都是资本家或他们的代理人，实行的也主要是支持他们的垄断集团的政策，而普通民众根本无力与之竞争，很难在竞选中取胜，其根本的原因就在于金元民主，最后决定选举胜负的是金钱。正因为这样，资本主义国家的普通民众，尤其是下层的群众对金钱操纵的政治游戏逐步失去兴趣，参加议会和总统选举的人数越来越少，美国总统大选的选民投票率当前只有50%左右。这再清楚不过地表明，西方资本主义民主只不过是一些垄断集团的民主，是他们之间政治上的争权夺利。<br />
<br />
　　中国特色社会主义民主则与西方资本主义民主根本不同。新中国成立后，我们建立了人民当家作主的人民政权，进行了生产资料的社会主义改造，消灭了剥削阶级和剥削制度，从而根除了引起人们在民主权利方面不平等的阶级根源，把只有少数富人才能真正享受的资产阶级民主，变为广大人民群众都可以真正享受的社会主义民主。应当承认，在我国，人们的富裕程度和其他许多情况是存在差别的，但这并没有成为人们独立、自由、平等地行使民主权利的障碍，因为中国特色社会主义是坚持以人为本的，也就是说是以最广大人民群众为本的。人和物的关系在我们这里和西方资本主义国家是根本不同的。我们的物质生产和精神生产都是为了满足人民群众日益增长的物质文化需要，而决不是像资本主义私有制国家那样以物来支配人，任何人都不允许用金钱来左右政治，干涉别人独立、自由、平等地行使自己的民主权利。人们不分民族、种族、性别、职业、家庭出身、宗教信仰、教育程度、财产状况和居住期限，都有选举权和被选举权，都可以享受到公民的各种民主权利，广大人民享受的民主权利是真实可靠的，是形式与本质相符的，是真正的人民民主。<br />
<br />
　　三、人民代表大会一院制，还是三权分立两院制？<br />
<br />
　　在西方资本主义国家，政治权力是由不同的国家机构分别行使的，其中议会行使立法权，总统和政府行使行政权，法院行使司法权，形成了三权分立的多元权力结构。这三个机构地位是平等的，彼此间没有从属关系；权力是平行运行的，彼此间相互制约、相互监督。西方资本主义国家的议会一般分为上议院、下议院或参议院、众议院，同样也是彼此地位平等而且相互制约，按多元方式行使立法权，被称为两院制。三权分立和两院制对反对封建专制主义、防止权力高度集中发挥了重要作用，其中体现的权力制衡与监督原则，是对人类民主政治发展的积极贡献。但是这种政治制度也存在着明显的内在缺陷，那就是权力多元行使所引起的各权力机关之间的互相扯皮、互相掣肘和政治权力运行效率不高、成本昂贵。这种缺陷在西方资本主义国家三权分立体制下是无法克服的。<br />
<br />
　　我国是人民当家作主的国家，国家权力由人民选举产生的全国人民代表大会统一行使。人民代表大会直接行使立法权、监督权、重要人事任免权、重大事项决策权，国务院行使的行政权和最高人民法院、最高人民检察院行使的司法权都是人民代表大会授予的，它们都要向人民代表大会负责、定期向它报告工作并接受它的监督。国家的这种一元化权力结构是由我国的社会主义性质决定的，它体现了国家一切权力来自人民、一切权力属于人民的原则，有利于保证和实现人民群众当家作主。在权力运行方面，我们与西方资本主义国家也根本不同，人民代表大会是按照民主集中制原则集体行使权力的，重大问题都是由人大代表或常委会委员在集体讨论的基础上按照少数服从多数的原则作出决定，既充分发扬了民主，又避免了西方国家三权分立的互相牵制、互相扯皮，以及行政权膨胀和议会变成“清谈馆”之类的弊端，把民主与集中有机地结合起来了，把民主和效率成功地统一起来了。<br />
<br />
　　四、共产党领导的多党合作，还是多党轮流执政？<br />
<br />
　　在资本主义社会，代表不同利益集团的各个政党在选举中进行激烈竞争，由取得选举胜利的政党或政党联盟上台执政，而在竞选中遭到失败的政党则成为反对党，对执政党进行制衡和监督，这样就形成了两个或多个政党通过竞选轮流上台执政的局面。两党或多党竞争可以对资产阶级政党产生巨大的压力和动力，有利于维护资产阶级的政治统治。然而多党竞争、轮流执政又存在着无法克服的问题。首先，每个政党所代表的只是某一个或部分资本家集团的利益，无论哪个政党上台执政，都必然首先维护支持他们上台的资本集团的利益，不可能完全公平地对待其他社会力量，而广大劳动人民只不过是他们竞选时利用和拉拢的对象而已。其次，竞争是西方政党关系的本质和主流，这种相互竞争严重影响了政党之间的团结与合作，进而影响和削弱了全社会整体力量的凝聚和发挥。再次，制衡和监督本来是民主的必要条件，但由于西方国家的制衡与监督是建立在政党竞争的基础上，就使这种制衡与监督具有严重的政党偏见，容易失去对事物判断的客观性和公正性，往往是“你赞成的我就反对，你反对的我就赞成”，致使制衡与监督变成了相互间的攻讦与掣肘。<br />
<br />
　　我国的政党制度是中国共产党领导的多党合作和政治协商制度，与西方资本主义国家的两党或多党轮流执政制度有着根本的区别。首先,合作共赢是我国政党制度的鲜明属性。社会主义社会不存在阶级对立，全体人民的根本利益是一致的，这构成了政党合作的坚实基础，使多党合作成为可能。社会主义社会各阶层人民的具体利益又存在着差别，需要政党来反映这种不同的利益和要求，这种差异性和多样性又使多党合作成为必要。社会主义社会的政党关系，不是资本主义社会那种对立的或者竞争的关系，而是友好合作的关系，各政党团结合作，群策群力，和谐相处，共同为实现全国人民的利益而奋斗。同时又尊重差异、包容多样，重视各阶层人民的不同利益和要求，重视各个政党的不同地位和作用，坚持全国人民根本利益与各阶层人民具体利益的统一，使各阶层人民在为中国特色社会主义事业共同奋斗中互利共赢，使各党派在多党合作中长期共存、共同发展。其次，共产党领导和执政是我国政党制度的核心内容。多党合作需要有一个坚强的领导核心，才能长期稳定地向前发展。中国共产党是以马克思主义为指导的无产阶级政党，是眼光最远大、胸怀最广阔、政治上最先进的政党，能够团结和带领各民主党派与无党派为中国人民的利益共同奋斗，从而保证我国多党合作的正确方向和坚强团结。坚持共产党领导与发挥民主党派作用是相辅相成、有机结合的。经过长期的实践和探索，我国已经形成了共产党领导、多党派合作，共产党执政、多党派参政的和谐政党关系格局，创立了中国特色社会主义的新型政党制度模式。再次，民主监督是我国政党制度的突出特征。我们的政党监督是相互合作的友好政党间的民主监督，监督的目的不是出于本党的私利，而是为了共同实现好中国人民的利益，这就使彼此的监督具有真诚的、不带偏见的、积极建设的性质，从而避免了西方资本主义政党为了竞争需要而难以克服的相互拆台、相互攻讦、相互掣肘之类的弊端。<br />
<br />
　　五、协商民主与选举民主相结合，还是代议制的选举民主？<br />
<br />
　　在民主的实现形式方面，西方资本主义国家普遍采用代议制的间接民主，由选民通过投票选举产生国家权力机关，再由它们代表选民来行使国家权力。这就使投票选举在代议制民主中具有十分重要的意义，有人把这种民主称为选举民主或票决民主。但代议制的选举民主存在着内在的缺陷：选民只能通过投票来决定谁代表他们行使国家权力，而不可能自己直接参与国家权力的行使，他们的民主权力实际上就是隔几年参加一次选举投票。各利益集团在竞选中利用金钱操纵选举，进一步放大了代议制选举民主的这种问题和缺陷。西方国家的一些政治学者针对代议制民主的困境和问题，提出过多种构建协商民主的设想，试图对西方代议制选举民主进行修补和替代，从而引起了人们对西方选举民主的质疑和对协商民主的注意。<br />
<br />
　　中国特色社会主义的民主形式是选举民主与协商民主相结合。我国的主要民主形式是选举民主。宪法规定，最高国家权力机关全国人民代表大会按照选举民主规则运行，各级人民代表大会的代表和领导机构是选举产生的，人民代表大会的各项决议是投票表决通过的。另外，我国有长期形成的中国特色协商民主，这种协商民主与选举民主是有机结合的。我国的协商民主主要通过两条渠道来进行：一是政党之间的协商。共产党作为执政党，经常就国家重大问题直接与参政的民主党派和无党派协商对话，充分听取他们的意见，这些协商意见被吸收到执政党提交给全国人民代表大会的建议中，作为人民代表大会决策和立法的基础。二是政协会议的协商。政协是历史形成的专门进行政治协商的组织，在政治协商会议上，共产党和各民主党派、人民团体、无党派人士及社会各界的代表人士，就国家重大问题进行协商讨论，这些协商成果作为政协的决议和议案直接提交给人民代表大会，作为人大制定法律和进行决策的重要依据。此外，我国每年一次同时举行的人大会议与政协会议，政协委员就人大讨论的问题充分发表意见，为人大的最后决策提供了直接的参考。党的十七大明确提出，要把政治协商纳入决策程序，这进一步推进了我国两种民主形式的结合，使党和国家的重大决策建立在充分政治协商的基础上，从程序上实现了我国根本政治制度与基本政治制度的成功对接，不但扩大了公民的政治参与，拓展了民主的社会基础，而且增强了决策的科学性，提高了决策水平，从根本上克服了单纯实行选举民主所难以避免的缺陷。<br />
<br />
　　中国特色社会主义民主是从中国实际出发形成的社会主义民主，是比西方资本主义民主更高类型的民主，具有自己的制度优势和鲜明特点。由于中国特色社会主义民主诞生的时间还比较短，要达到完善和成熟尚需一个长期的过程。我们必须把坚持与发展中国特色社会主义民主统一起来，坚定不移地走中国特色社会主义民主政治发展道路，不断总结人民创造的民主建设经验，既吸收和借鉴人类政治文明成果，包括西方资本主义民主中一切对我们有益和有用的东西，又坚决反对照搬西方资本主义民主模式，同西方资本主义民主划清界限，积极稳妥地发展中国特色社会主义民主，并在实践中展现其更加强大的生命力和吸引力。<br />
<br />
　　（课题组成员：闫志民　常光民　杨绍华　李传柱　王传志）</div>

							
							
							
							
