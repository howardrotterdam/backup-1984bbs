																											<div id="postmessage_833376" class="t_msgfont">笑观“求是”化“秋石”<br />
<br />
章立凡<br />
<br />
&nbsp; &nbsp; 最近读到《求是》杂志一篇署名“秋石”的文章，题为《为什么必须坚持人民代表大会制度而不能搞“三权分立”》，不禁哑然失笑，冒出一句粗话――忒能搞球了！<br />
<br />
&nbsp; &nbsp; “秋石”者，“求是”之谐音也。文末注明是一个四人写作组，没有“本文不代表本刊观点”之类的“郑重声明”。根据官家文宣传统，以这类谐音署名的，多是官养秀才班子。攒出点官样文章就无须哂笑了吧，可这署名也忒“山寨”了，莫非真个――“秀才不读书”？<br />
<br />
&nbsp; &nbsp; 为何说他搞笑，要看“秋石”是个啥东东？说白了，就是一种从童男童女尿液中萃取提炼的春药。古代方士尝以此药进贡皇上，据说服之可以“长生不老”。醉心于中国道家房中术的英国学者李约瑟，曾在与鲁桂珍合写的一篇论文中，指出秋石的作用相当于现代的性激素。他们将秋石列为中国古代科技的26项发明之一，而在欧洲，直到上世纪20年代才发现尿液中有性激素。<br />
<br />
&nbsp; &nbsp; 相传秋石的提炼，始于公元前2世纪西汉淮南王刘安；东汉炼丹家魏伯阳著《周易参同契》，有 “淮南炼秋石”的记载。自唐宋以来，此术开始风行，《道藏・大丹记》、《许真君石函记・日月雌雄论》等唐代丹书皆有论述；北宋沈括在《苏沈良方》中，详尽记录了秋石阴阳二炼法的程序要诀，其后叶梦得在《水云录》也有记载。这两种方法是：<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 阳炼法，用人尿十余石，各用木桶盛，每石入皂荚汁一碗，竹杖急搅百千下，候澄，去清留ぃ并作一桶，如前搅澄，取浓汁一、二斗，滤净，入锅熬干，刮下捣细，再以清汤煮化，筲箕铺纸淋过再熬，如此数次，直待色白如雪方止，用沙盒固济，火煅成质，倾出，如药未成，更煅一、二次，候色如莹玉，细研，入沙盒内固济，顶火养七昼夜，取出摊土上，去火毒，为末。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 阴炼法，用人尿四、五石，以大缸盛，入新水一半，搅千回，澄定，去清留ぃ又入新水搅澄，直候无臭气，澄下如腻粉，方以曝干，刮下再研，以男儿乳和如膏，烈日硒干，如此九度，为末。<br />
<br />
&nbsp; &nbsp; 到了明代，又发明了石膏炼法和乳炼法；入清以后，此术渐衰。当今中药所用的秋石，主要是以盐为原料制备的赝品，俗称“咸秋石”。<br />
<br />
&nbsp; &nbsp; 中国古代早有饮人尿治病的经验，中医验方亦以人中白（尿碱）入药。李时珍在《本草纲目》中点评道：“古人惟取人中白、人尿治病，取其散血、滋阴、降火、杀虫、解毒之功也，王公贵人恶其不洁，方士遂以人中白设法锻炼，治为秋石。”至于为何以“秋石”名之，李时珍解释说：“淮南子丹成，号曰秋石，言其色白质坚也”。<br />
<br />
&nbsp; &nbsp; 原来如此！从大众化的直接灌尿服尿碱，到贵族化的萃服尿中精华，方士们进贡的秋石，迎合了统治者的虚荣心态，堪称高级尿制品。<br />
<br />
&nbsp; &nbsp; 秋石的功效究竟如何？李约瑟认为：“在10－16世纪之间，中国的医药化学家以中国传统式理论（而不是以近代科学的理论）作指导，从大量的尿中，成功地制备了较为纯净的（in relatively purified form）雄性激素和雌性激素混合制剂，并用它们治疗性功能衰弱者。”但李氏此说，也一直有学者质疑。<br />
<br />
&nbsp; &nbsp; 唐代诗人白居易与元稹（字微之）为诗友，元耽于此术，年五十三而卒。白氏曾有“微之炼秋石，未老身溘然”的诗句怀之，可见秋石作为一种壮阳药，很是透支精力。李时珍在《本草纲目》中更鄙夷地说：“服者多是淫欲之人，藉此放肆，虚阳妄作，真水愈涸，安得不渴邪？况甚则加以阳药，助其邪火乎？”<br />
<br />
&nbsp; &nbsp; 其中最邪火的人物，即二十多年不理朝政，沉缅于采阴补阳、炼丹长生的嘉靖皇帝。佞幸顾可学贿赂奸相严嵩，向圣上进献秋石，称服之可以长生。嘉靖以为有效，顾某夤缘时会，官至礼部尚书加太子太保。当时民间笑传：“千场万场尿，换得一尚书”，谑称 “尝尿官”、“秋石尚书”。御史何维柏曾弹劾严嵩、顾可学等，但嘉靖坚称“朕自服石”，降手谕安慰严嵩“卿当自信勿负焉”，将何下狱。<br />
