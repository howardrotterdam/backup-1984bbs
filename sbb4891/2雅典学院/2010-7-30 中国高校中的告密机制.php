													<h2>中国高校中的告密机制</h2>
						
						
																											<div id="postmessage_765656" class="t_msgfont">中国高校中的告密机制<br />
（四川）李文倩<br />
<br />
2008年我从四川某大学毕业，考上本校的研究生。因为是新生，对未来的研究生生活尚抱有幻想，想认真读点书，好好做一番“学问”。9月份开学后，我和S一起，想组织一个读书会，每周周末聚一次，自由讨论，砥砺思想。主题定为文艺、思想和学术。本科时，我们有一个文艺沙龙性质的小团体，取名为“天堂桌子”，几个朋友不定期聚会，有时乱逛，有时吃饭或喝茶，朗诵诗歌，争鸣时事，胡吹乱侃，不亦乐乎。因此之故，我们就直接想到“天堂桌子读书会”这一名号。读书会虽有这样一个名号，却无章程或宣言，是一个很松散的小圈子；除几个熟识的朋友经常来之外，其他的参与者，都是有一搭没一搭的，来去自由。读书会就这样开始了，从最初的3、5人，到后来的7、8个，大多时候10来个人，只有一两次超过了20人。<br />
<br />
第一学期很快就结束了，转眼间又是寒假。我不想回家，主要是想留在学校读点书。S和我住一个寝室，之前回家一趟，春节前又回校了。文学院通过学生干部，问有哪些学生寒假不回家，我和S说不回。学校快放假的时候，我们接到通知，说文学院党委书记T要见一下留校的同学，表示党组织和院领导对同学们的关切。记不清是哪一天了，我们几个留校的研究生到了T的办公室，当时辅导员L也过来了。T问大家为什么不回家，是不是有什么困难，如果实在没有回家的路费，学院是可以解决的，等等。有同学回答说是为写论文，有的说是想看看书，都表示没什么问题。这件事就这样结束了，我当时还以为这要么是他们每年的例行工作，要么是T刚当书记不久，想表示一下他对研究生的关心。后来我发现并非如此。研究生第二年，2009年之后的寒假，我仍然没回家过春节，T也没再“召见”留校的研究生，可见这不是每年的例行工作，而是特殊时期的非常举措。2009年是很特殊的一年，一系列的重大历史事件叠加在了一起：五四运动90周年，中华人民共和国建国60周年，西藏达赖流亡50周年，八九学生运动20周年，非法镇压法轮功10周年。当局对此非常敏感，拨巨资启动“6521”维稳工程，城市里的每个角落，都有便衣警察的身影。各个高校更是他们重点防范的对象，唯恐大学生上街，或出现其他什么意外；为此我们学校多次召开有关大学生思想政治教育的会议，主要还是怕出事。高校的BBS论坛，只要发一篇不合时宜的文章（哪怕是转帖的），要么很快被删除，要么有五毛党跟帖辱骂，“消除不良影响”。<br />
<br />
年前，学校组织没回家的学生吃饭，辅导员L也来了，很热情地问我为什么不回家。当时给我的感觉是，好像我不回家，给她造成了很大的负担。对她的热情，我同样感到莫名其妙。因为自上研究生之后的半年以来，我总共只见过她一两次，一次是开学注册时，一次好像在校园里碰到。而在一般情况下，总有不少学生（并不一定是学生干部）去亲近辅导员，好在她那里捞一瓢半点的汤汤水水（看得见的，是可以每年参加两次评卷，一次是普通高考，一次是成人高考，加起来有约3、4000的收入）；对那种听命于她的学生，她自然是笑脸相迎，而对我这种根本不“尿”她的学生，她一般都不闻不问。因此我对她的热情，感觉很不适应，但当时并未想太多。<br />
<br />
春节后学生们陆续回校，和我们住一个寝室的W也回来了。他是中国古代文学方向的研究生，中国共产党党员；平时很关心时政（他电脑屏幕上的背景，就是一幅“一定要收复台湾”的红黑色图片），是毛泽东的崇拜者。开学后，W带礼物（好像是酒）去找L和J（研究生教学秘书），说是要和学院的老师“搞好关系”。那天他送礼回来后，一直带着笑（他平时就爱笑），我也没太在意。稍过了一会儿，他一边玩电脑，一边笑着对我和S说：L刚才在问你们两个，说你们寒假在寝室干什么。我说你们都在看书。她又问你们平时都谈论些什么话题，跟什么人交往，有没有对共产党、政府有什么不满的言论。我说你们的朋友我不认识。有时对现实不满，发发牢骚的情况是有的，但也不经常这样。她还让我平时多注意你们的动向，一旦有什么苗头，要立即给她报告，并让我不要将此事告诉你们，等等。听W讲完，我才知道“老大哥”已在看着我了。之前曾听说各年级的辅导员给党员学生安排任务，要他们注意少数民族同学的动向，特别是藏族同学的一举一动。现在看来，这种制度化的告密行为，是存在于整个校园之中的。S后来有次去文学院办公室办什么事时，L告诫他说，让我们在读书会上不要谈论政治。这证实了我的一个推断：他们之所以对我和S不放心，主要是因为读书会的缘故。后来因为没出事，这件事似乎也就不了了之了。读书会继续进行，有时涉及政治，比如对《零八宪章》和刘晓波案的关注等。但多数时候，主要话题还是学术方面的。<br />
<br />
2009年9月，具体哪一天记不清了，大致开学没几天，我们还在搬寝室时，我接到学院办公室的电话，说让我去参加党校培训，问我能不能保证坚持去上课。我说不能保证，没时间参加什么党校培训，之后就关断了电话。他们又给S打电话，直接要他去交钱（好像是36元），参加党校培训，被S拒绝，当时Z也在场。后来听我们专业的一个女生说，她也和我们一样，都没交过入党申请书，也被莫名其妙地通知去参加党校培训。曾听西南政法大学的LXL说，他们学校的研究生，和我们这边的本科生差不多，都在积极争取入党。据朋友说，四川另一所大学的某学院，规定研究生如果是党员的，在评奖学金时直接加10分（普通期刊发一篇论文，只加0.5分）。记得当时几个朋友都还在说，入党就是“吃红烧肉”。现在看来，中国共产党采取主动的收编行动，至少已经延伸到了研究生层次。前两天，我在推特上看到消息说，以后如要进高校，必须是中共党员才行。如果这一政策真要实行，估计将有更多的高学历者加入中共。<br />
<br />
写到此处，想到钱理群在《知我者谓我心忧》一书的后记中写到八九之后北京大学的状况：“还有更为险恶的：学校曾发出一个所谓《加强政治思想工作的几条措施》的文件，发到每一个教师，我因此得以见到，其中一条让我看得目瞪口呆。说是为了预防发生六四这样的突发事件，要在学生中培养一批‘积极份子’，他们的任务有二，一是及时向校方和有关方面汇报学生中的‘动向’，二是在‘关键时刻’站出来，‘代表’北京大学学生表态，说话。其实就是建立一支‘特务学生’队伍，这是当年国民党对付共产党的办法，现在共产党把这个特务传统继承下来，并加以发展了。--我相信这样的举措，是得到有效的执行的，而且延续至今。”由此可以推断，60年来（文革之中，八九之后尤甚），全国所有的高校都有这种制度化的告密机制。我所经历的这件小事，不过是无数此类事件中的一件，也算是为钱理群的推论，做一个小小的注脚。而我之所以要记下这件小事，也是为了给历史留存一个生动的细节。<br />
<br />
（2010年3月9日）</div>

							
							
							
							
