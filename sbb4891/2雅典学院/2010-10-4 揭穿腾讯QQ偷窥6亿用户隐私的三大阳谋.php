　　事件回放：<br />
<br />
　　9月27日，深受3亿网民信赖的安全服务商360推出个人隐私保护工具“360隐私保护器”，监测某些秘密偷窥用户电脑中隐私文件的软件。根据用户投诉，该产品第一版曝光了投诉最多、覆盖量最大的聊天软件QQ。发布后引起网友热烈反映，不但纷纷上传自己隐私被偷窥的截图，并引发了全国媒体关注。<br />
<br />
　　不料，此举立即引起QQ强烈不满。腾讯CEO马化腾一改平日作风，亲自紧急召开高管闭门会议，距离“360隐私保护器”发布不到10小时就发出 “严正声明”，不但默认暗中扫描用户电脑，并称这么做是为了保护用户账号、虚拟财产和数据，并强调“我们有决心和信心”将维护用户的“安全”进行到底。<br />
<br />
　　为什么素以稳健著称的马化腾会大动肝火呢？因为“360隐私保护器”无情地间接曝光了他的三大战略”阳”谋。<br />
<br />
　　&nbsp; &nbsp; 第一，控制用户。搞互联网的人都明白，手里有客户就好办。而QQ已拥有6亿用户，同时在线人数超过1.5亿！理论上QQ可以分析几乎全中国用户使用电脑和上网的行为轨迹，进行数据“挖掘和分析”，并利用其QQ弹窗、网站广告、聊天窗口展示等手段推送精准定向广告。夸张一点说，QQ比你更懂你！根据腾讯最新财报，其2010上半年广告收入为6.019亿人民币元，比去年同期增长54.5%！为了保证其收入不断增长，QQ当然要连续扫描用户电脑隐私文件并进行 “分析”，从而牢牢控制用户。浑然不觉而无辜的用户，就这样被一个聊天软件挟持，沦为其牟取巨额利润的肉鸡！<br />
<br />
　　第二，监视对手。腾讯号称“客户端软件之王”，从中尝到了甜头，却不肯别人这么做。QQ对其它客户端软件一直非常警惕，随时准备进行打压。那么，最好的监视手段莫过于直接监视用户的电脑了！无论什么输入法、下载软件、视频播放软件，只要用户用了，QQ通过扫描用户电脑立即就会知道；甚至可以精确到用户何时安装、安装后主要使用了哪些功能、使用的具体数据。这一点，从网友上传的“QQ偷窥截图”中迅雷、暴风、搜狗输入法等客户端软件比例极高的原因。业界往往惊诧于QQ抄袭同类产品的相似性和“创新”的针对性，那是因为QQ在用户的电脑里搞研发！这哪里只是不正当竞争，简直就是孙悟空钻进了铁扇公主的肚子，马化腾真是把“知彼知己，百战百胜”用绝了！<br />
<br />
