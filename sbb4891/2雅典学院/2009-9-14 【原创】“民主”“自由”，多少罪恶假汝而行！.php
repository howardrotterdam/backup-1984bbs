																											<div id="postmessage_240690" class="t_msgfont">“民主”“自由”，多少罪恶假汝而行<br />
在写下这个看似脑残而且欠扁的题目之前，我已经做好了挨鸡蛋、唾沫以及西红柿的准备。<br />
让我痛下决心写下这篇文章的，是一个网友的简单问题：“你说民主制度不错是吧？那请问古希腊的民主制度如何？苏格拉底的死不是被投票投出来的么？”呃，我得说，这个问题“too simple，too naive”了，古希腊的“直接民主”、“多数派暴政”与当今国际主流的“间接民主”、“代议制民主”之间的关系，基本接近于黑猩猩和人类的关系，反倒与文革式的“民粹民主”相当接近。<br />
然而这问题为何引起如此多的误解？依我之见，关键在于概念的含混不清。如果想认真地讨论问题，那么必须给出足够清晰的定义，那么不妨将“民主”定义为“多数派对少数派的强制”，而“自由”则可定义为“免于强制的权利”（主要指少数派），据个例子，10个人中6个想打乒乓球，4个想游泳，如果按照民主的原则，那就是大家一起去打乒乓球，必然会有强制，这时候一个人跳出来说：“我想去游泳，这是我的自由。”这个就是民主和自由最原始也最清晰的定义。不少作家笔下的“民主”则包含了“自由、法治、宪政、人权”等诸多概念，将“民主”打造成一种至善至美的乌托邦以吸引读者，我认为这是不负责任的，这只会造成更多的误解。奥巴马这样演讲是没问题的，因为美国民众理解他所讲的“民主”是广义而非狭义，而在中国则最好不要这样讲，因为中国老百姓暂时还只懂柴米油盐，选票是个稀罕玩意儿，说实话，除了学生会选举，我还真没见过。<br />
所以，今天我要还“民主”、“自由”本来面目，并告诉大家，这两个不是救世主，而且可能变成恶魔！<br />
一、“民主”好邪恶<br />
首先，美国是“民主国家”吗？<br />
这是一个愤青们经常嚷嚷的话题，他们的论点主要有以下几个：1、美国选秀式的民主选不出贤才（稍高级一点的愤青还会举出“阿罗不可能性定理”，虽然他们未必理解这个定理）；2、“金钱民主”不能代表民意（高教社版《马克思主义原理基本概论》第156页第三行，作为一个马原没挂科的人，我对这句话记忆蛮深刻）。<br />
好吧，我承认，美国不是“民主国家”，但很遗憾地告诉你们，美国是“自由国家”。<br />
&nbsp; &nbsp; 我翻遍了《美国宪法》、《独立宣言》，愣是找不到“德先生”，倒是多次提到了“自由”(我倒是很想把她称为“富女士”，因为只有经济自由，才能国富民强)。<br />
&nbsp; &nbsp; 好，美国选举是一场选秀，甚至我们可以更狠毒一点，假定08年大选选出的不是奥巴马（题外话，白皮肤的卡斯特罗同志曾放言“美国这种没有民主的国家绝对不会让奥巴马上台的”），而是超女李宇春（本人绝无侮辱李宇春的意思，只是必须举出一个人名，如有冒犯玉米，请见谅）或者什么好男儿之类的，选出了李宇春又如何？在美国，即便李宇春当选了总统也没有权力按照农村千分之一、城市万分之五的比例镇压一批亵渎李宇春敢于去听周杰伦的”反革命分子 “（这个按比例镇压是谁的发明可以去找杨奎松老师关于镇反的文章）；不能在四处安上大喇叭放她的歌；不能在你的家门口、课本、户口本、结婚证上面贴满她的照片，不能……（此处有N句“躲猫猫”，欢迎扩展以上句式）而在另外一些很“民主”的国家，这些看起来像笑话的东西都是刚刚发生过的事情，他们的逻辑是，李宇春同志是民意的代表。<br />
为什么李宇春同志没有权力抓一批敢听周杰伦的人呢？因为听谁的歌是爷的自由！如果那样的话，民主就妨害了公民的自由，而这一点是美国宪法不允许的，宪法早就限制了总统的权力，西方立宪学说认为，任何权力都可能是洪水猛兽，包括民主权力，多数人不见得比少数人明智公正，所以无论多数人还是少数人，必须事先用宪法制定契约，否则，公民的自由就会受到民主的侵害。大卫&#8226;休谟曾有一个著名的政治哲学命题――“无赖原则”，他说：“必须把每个政府成员设想成无赖之徒”，这也是我们为什么有宪法的原因，防火防盗防师兄，这就是普世价值，而不是华科特色，“特色”这玩意儿只能说华科帅哥多点，武大美女多点，而“普世价值”则是：在这两所大学里面，基本上可以说，除了男人，就是女人（之所以说“基本”，是因为经常有神童窜入我校，不便称之为“男人”，请勿瞎想）。换言之，宪政的路径可以不同，但效果要一样。不能以“特色”、“国情”为由排斥宪政，只能说实现的方式可以不同。<br />
在中国，大多数人考虑的是“谁统治”的问题，可问题的症结却在于“怎样统治”。我们关注奥麦大战，驴象之争，却忽略了，无论是奥巴马还是麦凯恩上台，他们都无权凌驾于宪法之上，无权干涉公民自由，他们被注视，他们在裸奔，像尼克松那样稍有不慎就被“深喉”拉下马来（其实陈水扁被判刑也是台湾的进步，中华的骄傲），思考“谁统治”往往等同于“谁来当皇上？奴才跟谁混比较爽？”，而思考“怎样统治”则可能得出“我们不要皇帝，我们干脆让宪法当‘皇帝’吧”，英国大宪章“王在法下”的原则也就在此。换言之，国际主流（注意，不是传说中的“西方”哦）的“宪政民主”中，“民主”仅仅决定权力的来源――“谁统治”，而“宪政”则决定权力的界限、运行――“怎样统治”，在我看来，后者显然更重要。暴政的对立面绝不是民主，而是宪政。这里也可看出东西方思维不同，东方想的是把简单的问题复杂化，怎样才能选出一个“圣人”来带领我们前进，把法律问题政治化；而西方却想把复杂的问题简单化，把几乎一切政治问题用法律的手段解决，即使奥巴马真是花瓶，也不会出乱子。<br />
在中国，常见的第二个误区是“逍遥法外”，大概很多人认为法律和自由是不相容的，刚还看到一哥们儿在网上的留言“索马里比美国更自由，可以拿着火箭筒逛街”，为什么这哥们儿不想想“拿着火箭筒逛街”是侵犯了别人的自由呢？一彪形大汉拿着个火箭筒在东九站着，我敢进去么？这不就是侵犯我的自习的自由么（想到此倒是有些惭愧，有这个自由却一直没好好利用）？而这个在西方却是共识，自由和宪政法治有着必然的联系，热爱自由的西方人也以法律为骄傲，西塞罗有个成语叫做 Libertas in legibus, 就是说自由因法律而存在，希腊人曾对波斯人说：“我们是希腊人，我们有法律，这是我们的优越性。”拿破仑临死前曾声称：“我用以传之万世的，不是我的政绩和武功， 而是拿破仑法典。”而在中国的传统里面，不喜欢谈法治，而希望靠礼节、伦理那一套维系社会，也不希望人们有太多自由，有位教授（貌似还是我们学校的？）说过，中国与西方相比，差的就是“law”，指的是宪政法治和自然科学规律，有趣的是这两个恰好是有精确界限的，一就是一，二就是二，枪毙就是枪毙，和艺术、诗歌这些东西有着本质的不同，三肯定大于二，但李白和杜甫哪个牛一点就不肯定了。而这一点则非常重要，其意义甚至超过了“自由”，下文我还将谈到这方面一点粗浅的知识，却是本文的关键。<br />
宪法，作为政府与人民订立的神圣契约，厘定了公民的权利、自由和政府的责任、权限，用宪法约束民主政府，以保障自由，法无明文规定，皆为公民自由，政府不得干涉；法无明文规定，政府皆不可为。而不能用“通知”、“红头文件”代替法律，甚至凌驾于宪法之上。话说河南省南阳市公安局直属分局（我也学习西方，把单位名搞精确一点）曾经对一位公民开出了1900元罚款，理由竟是此人电脑中有些少儿不宜的视频（同学们保护好自己的电脑啊），由于法律无明文规定，这个处罚显然是可笑的，当然，在媒体的关注下，此处罚被撤销，这也从另一角度看出，媒体多么重要啊，媒体让我们的公仆裸奔，只有这样，我们的自由才能真正被宪法保护。<br />
从以上的分析可以看出，美国确实不如“大鸣大放大字报”的文革“民主”，但正是通过严格甚至刻板的宪政法治才实现了我们老祖宗幻想的“中庸”，达到了自由和民主的制衡，多数派和少数派的平等相处，避免了各方直接冲突，避免了“51%枪毙49%”的惨剧，这个思路比我们曾奉行的“阶级斗争”不知先进多少倍，共和的理想也由民主宪政实现。而从另一个角度来说，古儒梦想实现一种不偏不倚的中庸之道，于是选择了刚性不够强的“礼治”，皇帝随时可以以“仁义礼智信”、“天道”等冠冕堂皇的理由蔑视法律，法律本身对最高统治者亦不起作用，法律成了一门“艺术”，实际上造成了弱肉强食的朝代周期。以刚求柔，刚柔兼备；以柔取刚，刚柔尽失。<br />
而那种直接的“最民主的制度”，离开了自由的限制，造成了两方甚至多方死掐、武斗，与间接的“宪政民主”相比究竟孰优孰劣？最民主的民主不是民主，一方面，政治是个专业活儿，不是人人都能做的（不过等您读完我这篇，去干干政治也不错哈），另一方面，人多力量大，人一多了就猖狂，就对少数人施暴，突破法律的框架变得很容易，“法不责众”嘛，宪政也就成了泡影。苏格拉底也就是这样死的。本来，“民主”就是一个左派词汇，但在这片神奇的土地上却被打上了“右”的标记，实在吊诡，个中玄机，读者自行参透。<br />
至于有人认为美国选举是“金钱选举”，我又得承认，美国虽然划定了捐款上限，但其官员平民化的比例仍远不及北欧、西欧，但蛮搞笑的是，一个跑了一百步的逃兵能去笑跑了五十步的么？何况“美国”仅仅是名称里面有个“美”罢了，她在我心中绝不是完美的，甚至在我看来，其改进的空间还有很大很大，只是大多数中国人的视野只放在美国上，却遗漏了欧洲。但从另一方面来讲，在大萧条时期，征收超高的累进税、遗产税，严重损害大财团、“大资本家”（容我这么说说）利益的罗斯福，仍然连选连任，直到今天，左翼的民主党仍然屡次击败右翼的共和党（如果你不懂这两个党的区别只能说明你看政治教科书、《中国不高兴》、《货币战争》这些脑残著作太多了，那么从今天开始，多读我的文章吧！），不正说明了这一问题并没有想象中的那样严重吗？正如在足球比赛中，黑哨起决定性作用的前提是两支球队势均力敌。而从经济学的角度讲，我甚至怀疑（再三声明只是怀疑而不是肯定！本人的左翼倾向根深蒂固，根正苗红，而科斯定理对我的吸引力仍然有限，远不及社会主义理想对我的吸引，实话），这种金钱政治可能是有一定好处的，防范多数人对少数人财产的掠夺，也许对保护产权，促进经济发展有着重要的含义，这也是张五常认为民主投票必须有一个上佳的宪法维护自由和权益的原因，正如你的钱不能由一群乞丐投票决定归谁一样。<br />
阿罗不可能性定理不过是说不存在完美的征集民意的方式，那么，世界上本无绝对纯净的水，大家能不喝水吗？从侧面来看，阿罗因这个定理获得诺贝尔经济学奖，不正说明了愤青眼里的“西方”是公正无私的吗？而对照一下老大哥苏联，将生物理论划分为“唯物主义的进步的生命理论”以及“反动的资产阶级生物学”，岂不是可笑？生科院的同学们，你们学习的孟德尔遗产定律在李森科看起来便是如此的反动！<br />
英国在17世纪便是典型的宪政国家，以“大宪章”为主的法律条文限制王权（大宪章的历史则有接近八百年），保障公民自由，19世纪才成为民主国家。<br />
杨小凯说：“ 一个国家的主流意识形态只讲民主不讲共和和自由主义，这个国家往往形成统治阶级和被统治阶级两级，往往在革命产生暴君暴君导致革命之间循环，而主流意识形态既有共和主义又有民主主义的国家往往是政治结构的三角或多角鼎立，国家比较安定。”<br />
法国大革命则与英美两国相反，是典型的“先民主后自由”，各派之间不是靠规则来制衡，而是内斗，大的吃掉小的，结果动荡不安。“先自由后民主则自由民主兼有，先民主后自由则民主自由尽失。”<br />
从另一方面来讲“先自由后民主”也往往是自然的趋势，例如近代袁世凯的行政也是从经济自由开始（经济自由自然又是政治自由的第一步），举世瞩目的“中国奇迹”的一个极重要原因便是解放思想，经济自由的结果。Hu Jintao总书记在法国国民议会向全世界做出了庄严承诺：“发展社会主义民主政治，是我们始终不渝的奋斗目标。我们明确提出，没有民主就没有社会主义，就没有社会主义现代化。”也充分说明了在经济自由后进一步深化改革的必要性。<br />
从上面的分析可以看出，“民主”非但不是救世主，而且一旦离开了“自由”的束缚，极有可能变成恶魔！<br />
二、“自由”蛮可怕<br />
就在答案似乎已经得出时，法国大革命中被斩首的罗兰夫人向我提出了抗议，而她的抗议词则是那句著名的“自由自由，多少罪恶假汝而行！”<br />
今年是法国大革命220周年，那场大革命的口号正是著名的“自由、平等、博爱”，平等和民主在很大意义上是重合的（穷人往往希望通过投票增加累进税、遗产税，扩大公共福利，形成一个平等的社会，有谚云“莫在瑞典做富人，莫在中国做穷人”），单从口号来讲，似乎并不缺少自由制衡下的民主。<br />
可是罗兰夫人们的自由乃至人身权利又由谁来保障呢？各派之间平等对话的博弈机制又在哪里呢？<br />
在我寻找答案时，《三国演义》给我提了个醒，第六回，董卓迁都扰民时的台词：“吾为天下计，岂惜小民哉！”<br />
我确信我找到原因了。<br />
“小民”是什么呢？绝对不是“人民”，因为我深信董太师的“天下”是可以包括“人民”的，因为他除了爱财宝、权势，也爱武士、美人，而他随时可以说：“剩下的那些就是人民，至于我杀的那些，统统不算！”作为太师，当然一言九鼎，其他人算什么“人民”呢？小民是能精确到个人的张三李四王二麻子，乃至罗兰夫人。<br />
这么看来，当“自由”变成一种“崇高的理想”，“伟大的使命”时，它就缺少了保护“精确到个人”的“小民”（否则便是“公民”）的力量。但如果我们用精确的法律来厘清权责，譬如，韵苑篮球场的第三个框子包括下面那块地归1984bbs组员张三所有，他有处置这框子的自由，看似平常，实际上才能让“自由”发挥作用，而如果把话说得不清不楚，“韵苑篮球场某一个框子归张三所有”，结果，哪个框子呢？再说了，中国的张三没有一千个，也有八百个，到底归谁？那不就归老大，归政府么？随时可能被强拆，“自由”就很辶恕<br />
这种精确的划分，从政治学来说，是“群己域的划分”，从经济学来说，是“科斯定理”。<br />
那么我们重新来审视“民主之罪”，民主和自由都可能导致暴政，原因是具体的方案被空洞的口号架空，引起民众的非理性狂热，使得局势不可控，如果脱离理性框架，核电站也许就变成了核武器，于是我们又可以看出，“民主”之所以比“自由”更危险，就在于前者离“个人本位”更远，更容易煽动老百姓的狂热。学者要做的事情是把每个概念剥离出来，严格分析其利弊，而非一味歌颂赞美，否则便是又犯了我上面提过的“古儒之过”。可见把“民主”“自由”打造成乌托邦幻想是一件多么不负责任的事情，乌托邦幻想是宗教的习惯，而不是世俗政治应有之物，其原因以后的文章会谈到。<br />
将罗兰夫人的名言推而广之，除“民主”“自由”外，一切没有精确定义、可行性方案的口号，越是冠冕堂皇越有煽动性就越可能成为切尔贝诺核电站。由于不同人对“爱国”的理解不同，“爱国爱国，多少罪恶假汝而行！”其实更加合适，其可怕程度也超过“民主”“自由”一万倍（抱歉，这个数字没有精确量度的含义，我也犯了“古儒之过”）。在我国历史上的极左时期，一些人往往会因为一些小事被轻判“人民公敌”，带上“反革命”的大帽子，由于“人民”的不确定性，咱们随时可能就不属于“人民”了，那么不也可以说“人民人民，多少罪恶假汝而行”么？而“公民”虽呆板，但美好得多，带上张身份证，爷就是公民，受宪法保护！<br />
不过说到这，又想起了孙志刚，由于没带身份证，又没办暂住证，被逮到收容所，后被殴打致死，俞江、腾彪、许志永等三位法学博士以普通公民的身份“上书”全国人大常委会，建议对国务院1982年5月12日颁布的《城市流浪乞讨人员收容遣送办法》进行违宪审查，这在中华人民共和国历史上还是第一次，程序未及启动，恶法已被国务院废除，所以仍应看到，中国宪政之路仍然漫长。<br />
我们的视角不应该只停留在“大国崛起”、“中华复兴”这类忽略个人的口号上，而应该关注我们身边具体的人和事。我们身边的邓玉娇能否不被侮辱？七十码的惨剧能否少一点？结石宝宝的未来怎么办？如果地震再来了，豆腐渣校舍扛得住吗？<br />
一位朋友跟我说：“中国的问题太多太多，可最大的问题是，几乎没有人愿意耐着心一个一个地解决问题。”<br />
“民主”、“自由”作为口号都是进步的，但如果它们只是口号，那么也许只能煽动起一时的狂热，而不是理性的热情。前者是核弹，后者是核电站。离开了“个人本位”，任何口号都可能是野心的伪装，即使开放的知识分子，也难免有好心做错事的时候，社会各层面理性的互动才是出路所在，这大概就是胡适先生说的“宽容比自由更重要”的真谛所在吧？<br />
