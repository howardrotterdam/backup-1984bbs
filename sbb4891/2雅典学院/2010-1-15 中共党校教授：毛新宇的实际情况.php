 <br />
由于没在党校获得“博士”学位，2000年邵华又把毛新宇送进军事科学院“攻读博士学位”，并在2003年在军事科学院如愿以偿。<br />
<br />
这也让一班党校教授对军事科学院很不以为然。<br />
 <br />
所以，现在这个根本“智力不健全”的毛新宇竟然出任军事科学院研究员，并任中国人民解放军军事科学院战争理论和战略研究部副部长、中华全国青年联合会常委、全国政协委员。2008年7月，还被任命为军事科学院战略部副部长，走上高级领导岗位，现为副军级，大校军衔，并很可能在今年升任“少将”。<br />
