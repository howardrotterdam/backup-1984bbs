													<h2>一篇有趣的文章：祝东力-危机与应对：关于政府与社会关系的几点思考</h2>
						
						
																											<div id="postmessage_593041" class="t_msgfont">危机与应对：关于政府与社会关系的几点思考<br />
(2009-08-05 22:07:55)<br />
转载<br />
标签：<br />
杂谈<br />
&nbsp; &nbsp; &nbsp; &nbsp; 分类：新政治学<br />
<br />
　　当前，全球金融风暴，中国经济衰退，正同步展开。2005年以来，净出口对中国年度经济增长的贡献率均在20%左右。考虑到全球消费市场持续萎缩的可能性，今后几年内，中国的经济前景绝不乐观。未来，如果经济危机加剧，是否可能升级为社会危机，社会危机再进而升级为政治危机？这里的关键取决于政府与社会之间复杂的互动关系，这种关系，简单地说，即官民关系。在整体的社会形势和社会生态中，官民关系占据支配地位，是影响未来中国走向的主导因素。<br />
<br />
 <br />
<br />
一、历史是遗产，也是债务<br />
<br />
　　中国的中央集权制度建立于战国，成熟于秦汉。韩非子说：“上古竞于道德，中世逐于智谋，当今争于气力。”中央集权制度最大的优势，就是“集中力量办大事”，即整合与调动全社会的人、财、物等资源，以实现国家特定的战略目标。这就是“举国体制”，这种体制正是长期战争环境（所谓“争于气力”）的产物。<br />
<br />
　　相反相成的是，李悝、晁错所谓“五口之家”的小农经济，也形成于战国至秦汉时期，这并非偶然。皇权-官僚集团的强大国家机器，不仅统一了政治权力，而且垄断了意识形态话语权，此外还通过长期的抑商政策，压制商人阶层的成长，从而控制了社会绝大部分的经济剩余。这个强大的国家机器所面对的，必然是分散细弱而又数量庞大的小农。因此，所谓“大国政治”与“小农经济”不仅同时出现，而且互为因果。在这种皇权-官僚集团的统治之下，既缺少一个世袭的贵族阶层，也缺少一个独立的商人阶层。因此，国家政治权力的分割和分享，便失去了社会物质基础。<br />
<br />
　　古代中原作为季风区，旱涝灾害频繁；同时又靠近北亚草原带，易受游牧民族侵袭。而只有大一统的中央集权国家，才能统合全社会力量，以抵御自然灾害，并抗击北方蛮族持续不断的南下攻势。所以，毛泽东说：“中国统一，为河与外族进攻二事。分裂则二事皆不能办。”说到底，中国特定的安全形势与民生需求，是支撑这种中央集权制度的根本原因。<br />
<br />
　　另一方面，传统国家在整合全社会资源的同时，其上智下愚的制度文化对大多数社会群体，特别是基层民众，无疑是排斥的，所谓“劳心者治人，劳力者治于人”。所以在中国传统社会，皇权-官僚国家的意识形态动员往往只能到达士绅阶层，所谓“君子喻以义，小人喻以利”。小农对于国家，一般只是单方面地承担赋税徭役，既不享有什么政治权利，也不分享多少国家意识，所谓“民可使由之，不可使知之”。<br />
<br />
这种体制，往往由于皇权-官僚集团的日益腐朽，导致官逼民反或蛮族入侵，最终政权更迭，形成中国历史上周而复始的“王朝周期”。相比之下，传统中央集权制度在近代遭遇的挑战则非同以往。因为西方列强持续的打击，造成的不仅仅是政权的更迭，而是中国三千年的古典文明趋于瓦解。<br />
<br />
　　经过大约100年的失败、曲折和摸索，到1949年，中共实施了空前绝后的全民动员体制，建立了全能型国家。这种全能型国家，以干部和群众，即官民关系史无前例的根本改善为前提，将社会、家庭、个人统统纳入国家体制。凭借这一体制，中国走出了近代困境，完成了以下历史性任务：第一，实现国家独立和统一；第二，初步建立了现代工业体系；第三，打破了以美国为首的西方阵营的战略围堵。毛时代的计划经济和统制型社会曾经受到广泛批评。但是，由于实现了近代以来几代中国人为之奋斗的“救亡”主题的基本目标，所以，尽管有不少失误和挫折，毛时代全能型国家的合理性，却是不证自明的。<br />
<br />
　　全能型国家经100年近代大危机而形成。当1970年代以后，危机逐步化解，历史由非常时期回落到常规时期，由全民动员体制回归于精英治理体制，特别是，当城市中产阶级在市场经济环境下成长壮大，社会形势和社会生态的总体格局发生了根本变化的时候，以往的政治文化也就面临根本的调整。<br />
<br />
 <br />
<br />
二、苏东模式、东亚模式与中国<br />
<br />
　　商业是过剩产品的交换，因此，商业资本总是以相应的经济剩余为存在的前提。传统农耕社会，由于生产力水平的严重制约，不具备大量经济剩余，从而难以支撑商业资本的持续扩张，因此必然以“抑商”为基本国策――否则，商业资本的非理性扩张必然要以贫困人口的生存本身为代价。由于商人阶层无法成长，在传统国家与小农之间，便一向缺少一个成规模的中间阶层。工业社会，生产力的瓶颈得以突破，中间阶层的成长壮大与工业化同步，成为必然趋势。<br />
<br />
　　另一方面，在历史常规时期，随着和平年代的延续，政治领袖则越来越不具有以往的“卡里斯玛”特性（即所谓超凡魅力）。其实质在于，由于历史大危机结束，政治领导人不再能通过大危机时代的非凡考验而建立权威，官民关系的扁平化、民主化同样是必然趋势。<br />
<br />
西欧的历史不必说了。中间阶层的成长壮大和民主化趋势，无论在计划经济体制中，还是在市场经济环境下，均没有例外，并且从20世纪80年代以来，都导致了政权更迭。这些政权的更迭形成了两种模式，即“苏东模式”和“东亚模式”。<br />
<br />
　　苏东模式的实质是，在苏联、东欧地区，全能型国家经过几十年治理，积累了大量社会矛盾，难以化解。体制外的西化派知识分子与党内部分官员联手，在美国等西方势力支持下，利用城市群众运动，一举颠覆国家政权，更改社会制度。而东亚模式的实质则是，在菲律宾、韩国、台湾、印尼等国家和地区，威权政府实行市场经济，经过几十的经济增长，资产阶级羽翼丰满后，在美国的操控下通过民主运动和选举，接管政权。<br />
<br />
　　就中国来说，80年代末的政治风波可以看作是典型的苏东模式，在当时即被有效抑止。其后20年，在威权政府主导之下，市场经济迅速扩张，同样哺养了一个日益成长的“城市中产阶级”。这个群体的出现是一个新的变量，彻底改变了中国的社会形势和社会生态，值得特别关注。<br />
<br />
　　新世纪以来，“城市中产阶级”在执政党的核心文件中获得了一个专有名词，即所谓“新的社会阶层”。2001年7月，江泽民在建党八十周年的讲话中，这样界定了这个“新的社会阶层”的成分：“改革开放以来，我国的社会阶层构成发生了新的变化，出现了民营科技企业的创业人员和技术人员、受聘于外资企业的管理技术人员、个体户、私营企业主、中介组织的从业人员、自由职业人员等社会阶层。”2002年10月，江泽民的十六大报告一字不差地重复了这个界定。而 2007年10月胡锦涛所做的十七大报告，则省略了对这个阶层成分的描述，仅保留“新的社会阶层”这一专有名词，涉及该阶层的内容也从前面两个文件中的一个段落压缩为一句话：“鼓励新的社会阶层人士积极投身中国特色社会主义建设。”这个政治宣示口径上的“缓和”，被另一个经济法律领域的“突破”所抵偿―― 争议多年的《物权法》在十七大召开的几乎同一时间里终于正式施行了。<br />
<br />
　　本来，新中国建立伊始，曾包含多个阶级和阶层。国旗是国家的象征，以国旗为例。“五星红旗”上的一颗大星代表共产党，四颗小星分别代表工人、农民、小资产阶级和民族资产阶级。但是，1956年社会主义改造完成后，在社会经济结构中，民族资产阶级和小资产阶级作为阶级均已消失。此后，“党政军民学”、 “地富反坏右”囊括了中国社会各阶层，形成了敌-我、干-群这种单纯的二分法。而干部-群众关系，一旦抽去了“人民共和国”的意识形态内涵，在某种意义上便是传统国家与小农关系的翻版。“新社会阶层”的出现，则打破了这种单纯的二分法，改变了以往的社会阶层构成。问题在于，苏东模式破产之后，依托于城市中产阶级的东亚模式是否就能成功呢？<br />
<br />
 <br />
<br />
三、社会形势正在压缩选择的空间<br />
<br />
　　中国近代以来，“救亡”和“发展”是前后相继的两个主题或阶段。如果说，毛时代基本结束了“救亡”阶段的话，那么，目前，“发展”主题中还有城市化、西部开发、重化工业建设以及分配结构调整等重大项目有待完成。如果没有一个强势政府进行规划、组织、协调与统合，那么，“发展”主题将被搁置，与此同时，中国社会也将因长期经济停滞而导致各种矛盾爆发，而陷于内乱。<br />
<br />
　　另一方面，中国的国家认同，特别是“中华民族”这一实体的经济、社会和文化心理方面的建构，尚未最终完成。自20世纪初，梁启超等人提出“中华民族” 的概念后，其内涵从民国初年的汉满蒙回藏，发展为新中国成立后的56个民族（第56个民族基诺族于1979年正式确定）。应该说，100年来，中华民族仍在形成过程中。具体地说，目前，中国还不能主要依靠经济、社会和文化认同等非政治、非强力的纽带和手段，维系、凝聚广大西部地域――仅新疆、西藏和青海相加就占中国总面积的40%以上。鉴于这种情况，在相当长时间内，就仍需要维系一个强势政府，以避免重蹈前苏联、前南斯拉夫的覆辙。这一点毋庸置疑。<br />
<br />
　　上述两点，即中国的“发展”和“反分裂”，是支撑目前政治体制合法性的最根本的依据。而不必讳言，这个政治体制最关键的要素就是“党的领导”。因此，这个政治体制也可以表述为“党国一体”。<br />
<br />
　　但是，这种理性的考虑不会被多数人分享。当前面临的社会局面是，第一，权力腐化现象造成政府，尤其是地方政府与民众关系日益恶化，导致人们对政府的逆反似乎在不断积聚。远的不讲，去年以来的瓮安事件、石首事件引发的大规模群体骚乱，以及邓玉娇案在互联网上掀起的强烈反弹，均传达了社会心理的重要动向。<br />
<br />
　　第二，“新社会阶层”，特别是其外围群体的规模日益扩张――2008年全国高校毕业生达560多万，按此规模，每10年就增加5000万名大学以上受教育者。这个庞大群体，不会仅仅满足于经济状况的改善，与传统的体力劳动者相比，他们对自身社会政治权利的意识无疑会更为明确，并有着更强烈的政治参与的要求和能力。<br />
<br />
　　第三，冷战结束后，以个人主义价值观为基础的西方民主意识形态席卷全球。市场经济环境中，个人成为利益主体，人与人之间形成契约关系。计划经济时代干部-群众之间以“为人民服务”为基础的“恩义”关系，日益失去存在的土壤。市场经济的生活方式决定了“新社会阶层”及其外围群体更倾向于接受西方民主意识形态，信奉所谓“小政府、大社会”的模式。<br />
<br />
　　因此，一方面，是中国的“发展”和“反分裂”任务需要“党国一体”的架构作为保障。另一方面，现实的社会形势又倾向于削弱和限制这个政治架构。一方面，以往的政治文化需要根本调整。另一方面，目前又不具备根本调整的现实条件。在日益缩小的空间内，一直存在着两种相反的诉求。<br />
<br />
 <br />
<br />
四、政治自由派与民族主义左派<br />
<br />
　　第一个是政治自由派的选项，很简单，就是彻底抛弃党国一体的现行架构，全盘照搬西方政治制度，实行民主宪政、多党竞选、三权分立等。这种选项的问题在于，它不仅将实际中止国家的“发展”和“反分裂”这两个重大职能，而且，<strong>其最终也无助于消除权力腐化。</strong>“透明国际”是一个研究腐败问题最权威的国际非政府组织。2008年9月，“透明国际”公布了其最新年度的“清廉指数”排行榜。<font color="red"><strong>在180个国家和地区中，中国名列第72，不仅领先于泰国（第80）、印度（第85）、印尼（第126）、菲律宾（第141）等亚洲民主国家，而且领先于乌克兰（第134）、俄罗斯（第147）、白俄罗斯（第151）等前苏联转型国家。</strong></font>实际上，任何选举政治，如果没有一个稳定、中立、高效、清廉的公务员队伍作为保障，都不可能解决腐败问题，而且往往会陷入发展的陷阱或政局动荡的困境。<br />
<br />
　　正是基于上述理由，民族主义左派一向抵制传统自由派的方案，揭露西方选举政治的非普适特点，而主张大体保持现有体制。但问题在于，在权力腐化的趋势难以抑制的情况下，新社会阶层及其外围群体不可避免地将受到西方民主意识形态的支配，所形成的社会压力作为一种势能将日益积蓄。民族主义左派常以发展经济作为当前体制的合法性来源，以“绩效合法性”对抗“选举合法性”。选举政治的确问题多多，小布什与金正日相比，其才、胆、识、力无疑都不在一个层次上，但选举政治的合法性在现实舆论中却难以被替代。绩效合法性来源于“民以食为天”的传统时代，并不适用于现代社会。况且，80后一代（含70年代后期出生者）在信息开放和互联网时代渡过青春期，在未来几年将成为社会主流人群。届时，社会治理方式将怎样与他们的社会权利意识相适应？这是民族主义左派面临的难题。<br />
<br />
　　需要明确的是，在东亚模式行之有效的前述国家和地区，除了资产阶级的成长之外，还存在着一个必要条件，这就是美国的作用。实际上，政治博弈的双方，即当地威权政府和资产阶级，均在美国的操控之下。东亚国家或地区的政权易手，不过是美国因势利导地改变了对当地社会的控制方式而已。而中国的情况显然有所不同。因此，尽管中国的确在威权政治主导下发展市场经济，形成了一个新社会阶层，但由于不具备这个美国因素，因此，东亚模式在中国很难成功。中国需要急迫解决的，是怎样化解社会形势所形成的压力。<br />
<br />
 <br />
<br />
五、先为不可胜，以待敌之可胜<br />
<br />
　　实际上，西方民主制度包含两个相互矛盾的前提。一方面，市场经济决定了利益主体的高度多元化，导致不同利益集团竞选执政权。另一方面，<strong>民主体制又需要维持一个利益共同体</strong>，也就是说，人与人之间、各社会集团之间的利益分歧必须控制在一定限度之内，以至于可以用“投票方式”，而不必用“暴力方式”，来解决不同政策取向之间的冲突。一方面是市场经济中现实的利益分歧，另一方面是民主政治所要求的利益共同体，这个矛盾只有通过以下方式来解决：<strong>即维持一个昂贵的社会福利保障体系，使底层民众也能有一个温饱有余的生活水平，避免出现贫富两极分化的局面。</strong>这是在一个利益多元的共同体内成功实行民主制度的基本条件。<br />
<br />
　　因此，一旦这个经济基础出现问题，民主政治的那种稳定、有序和文明的日常表象就可能发生动摇。2005年，美国卡特里娜飓风横扫新奥尔良市，摧毁当地的基本生存条件，引发了大规模骚乱；同年，由于法国穆斯林移民社群的生存、就业条件持续恶化，因偶发事件，在克利希苏布瓦市同样导致了大规模骚乱。换句话说，当发达国家哪怕是局部的物质基础动摇的时候，民主宪政之类的制度文化竟形同虚设，并不能发挥预期的作用。<br />
<br />
　　西方发达世界归根结底由美国霸权所支撑，而美国霸权无非是由三个支柱所构成：一是高科技-军事霸权，二是美元-金融霸权，三是意识形态话语霸权。由美国次贷危机引发的金融风暴已使全世界陷入经济衰退，也导致美国的美元-金融霸权和意识形态话语霸权同时遭受重创。此次全球经济危机，可能标志着西方文明衰落期的加速。因此，随着其优势地位的丧失，建立在汲取全球资源和财富基础之上的西方民主社会，极有可能在几十年内逐步沦落。<br />
<br />
　　有鉴于此，未来二三十年，中国可能将处于历史的关键时期。换句话说，无论怎样困难，只要中国咬紧牙关，渡过这二三十年，就可能进入一个历史性的利好时期。那时，来自西方世界的政治、经济、军事和意识形态压力将根本缓解，中国将获得机会，进入因西方势力收缩而出现的国际空间，在更加平等的世界政治经济格局中获得与中国人口规模更相称的财富和资源份额，从而回馈国内社会，在此基础上，彻底解决“发展”和“国家认同”问题，并且更从容地调整自身的政治体制、经济模式、社会架构和文化形式。<br />
<br />
　　要想平稳地渡过这二三十年的历史关键期，中国所能采取的手段可分为基本面和技术面。<strong><font color="red">基本面是，政府必须痛下决心，集中力量，切实缩小贫富差距。</font></strong>这不仅能有效增加社会需求，提高消费对经济的支撑作用，扭转中国经济依靠国际市场“体外循环”的窘迫局面，而且，作为缓解社会矛盾的举措，它也是根本改善目前官民关系的基础。<strong>相应的技术面则是，在维持现有政治架构的前提下，不断开发、推出<font color="red">“民主政治消费品”</font>，例如类似于已有的市民听证会和市长接待日等，适应人们不断增长的社会权利意识，增强人们的政治参与感，以提高官民关系的亲和程度。</strong><br />
<br />
 <br />
<br />
六、总结的话<br />
<br />
　　我们必须摒弃浪漫主义的思想方式。目前，客观上没有条件回到共和国前30年的状态，也不允许冒社会动荡的风险大幅调整现有政治架构，不论这种调整的方向是朝左，还是向右。中国只能以缩小贫富差距和增强政治参与感的方式，本末兼顾地适当改善官民关系，渡过未来的历史关键时期，以迎接世界史的新变局。<br />
<br />
