<br />
祝东力在整篇文章里,没有提及与宪政民主同为核心的公民教育.维护民生发展福利缓解矛盾以安抚民众 + 缓慢稳定但是主动有意识的提供民主消费品以安抚知识精英,其实说穿了,只是权术.说到&quot;民主消费品&quot;,18世纪的法国,路易十六王朝,其宫廷里充满了各种启蒙思想和辩论,法国的知识精英们与贵族公开地的谈论着平等呀,自由呀这些大逆不道的东西,在我们中国人看来,这种安抚够意思了吧?结果如何呢?路易十六上了断头台,法国进入更恐怖的雅各宾专政时期.这就是没有公民教育导致的后果----公民缺乏理性,导致缺乏商量与妥协精神,接着导致冲突,最后导致混乱与恐怖.<br />
<br />
我并不反对赤匪发展福利以缓解社会矛盾,但想要从根本上解决问题,还是必须采用西方的政治制度,而且,必须进行公民教育.说穿了,其实就是制度与文化的双重推进.这两者无论哪一方滞后,都会严重掣肘另一方.<br />
