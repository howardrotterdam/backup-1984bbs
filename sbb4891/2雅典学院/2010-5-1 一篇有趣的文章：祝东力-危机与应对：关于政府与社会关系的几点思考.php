<br />
真是有心人，就应该早早脱离盲目给人贴标签的童稚状态，严肃的考虑对方提出的这些挑战，至少应该予以严肃思考和正面回应。<br />
<br />
并不是你给人家一顶党奴五毛之类的帽子，人家提出的问题和挑战就不存在了。<br />
<br />
比如，中国老百姓最关心的是什么？不是民主和自由的价值是否能得到实现，而是<br />
1）民生问题，福利保障；<br />
2）腐败问题，贫富差距及其带来的不公平的感觉；<br />
<br />
自由派人士的主张，要么对上述问题漠不关心，只是一味空喊正义自由价值一类的空中楼阁，忙着纪念林昭，老百姓关心么？就算林昭投胎转世当了中华民主国总统，且不说会不会陈水扁话，就算她个人操守不染，有助于解决上面的两个问题么？<br />
<br />
要么就是卖自由民主大力丸，盲目腔调只要实现了民主制度，腐败问题矿难问题贫富分化问题就会迎刃而解 -- 这种朴素的观点其实也正是很多出身社会底层的知识分子对自由民主真正感兴趣的原因。但这种看似直白的逻辑，并不一定经得起推敲，至少需要经过实证检验。比如，卖大力丸的经常拿发达国家说事，但却常常可以回避有更多的民主国家腐败不亚于甚至超过中国这个事实，如原文红子部分列举2008年透明国际的数字。要研讨民主制度同腐败的关系，你不能看<font color="red">不腐败的国家中有多少是民主国家</font>，你真正应该关注的应该是<font color="red">民主国家中有多少是腐败的，多少是不腐败的</font> -- 这样才有量化评估的基础，通过评价民主化的预期利益（假设存在可计算的民主国家腐败率，即可对民主的预期收益进行评估）然后再同中国民主化所带来的风险进行对比，这样才能进行有效的决策。<br />
<br />
