<br />
在回答这个问题之前,我认为应该先说明一下,纵观当今世界发达国家,其基本特征是政治上比较完善的宪政民主与经济上比较完善的市场经济,这两者之结合才能真正让一个国家走向富强.现在回过头来看一看文章作者列出的&quot;民主失败&quot;的国家,有哪一个在政治上与经济上符合了发达国家的标准?印度虽然在政治上走的民主道路,但经济方面却从1949年--1991年采用的&quot;国有企业&quot;的公有制经济,91年以后才开始向市场经济转型,而转型以后,经济增长大幅提高,这就是市场经济的功劳.<br />
