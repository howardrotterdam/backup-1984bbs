													<h2>若温家宝是中共总书记</h2>
						
						
																											<div id="postmessage_725392" class="t_msgfont">【蓟门云扬 撰文】 - 2010-07-06 <br />
<br />
　　看着温家宝忙碌的身影，有时不禁想，假若他是总书记，中国会是另一番景象吗？ <br />
<br />
　　所以发此联想，是因为无论就才学、胸襟、抱负，还是理想，温家宝举手投足之间，比胡锦涛更多一份“腹有诗书气自华”的领袖风范。 <br />
<br />
　　先说理想。胡在中央高层十八年了，谁见过他主动表达过自己的政治理想？若前面的十年他是王储，必须潜踪匿迹，尚可理解。但他这八年贵为中华第一人，多少总该表达一下自己的理想吧。还是没有。除了照本宣科就是不言笑，是最没有性格特点的一位皇帝。一个没有理想的官僚，只能是一个政客，而不是政治家。 <br />
<br />
　　温家宝无疑比胡锦涛多出一份令知识分子敬仰的人文色彩。无论是他吟诗言志，还是畅谈政改；无论他心系普世价值，还是体认公平；无论是他看望学术泰斗，还是纪念耀邦；无论他与老农促膝谈心，还是面对民间疾苦黯然落泪，都无不展现出他不同于其他中国官僚的人文理想。这种理想使他与胡比起来，更显示出一个政治家的胸襟。 <br />
<br />
　　次说才学。胡锦涛语调永远平缓如一，讲话永远干巴生硬。据说一天大部分时间就是在那儿背讲稿。就连在家里也难于流露出人性温情的一面，据中南海知情人透露，胡的夫人、儿女听他说话，都像听中央人民广播电台的新闻与首都报纸摘要节目一样。这样的人，有什么才学。 <br />
<br />
　　温家宝完全不同。总是能恰当地引经据典表达心意，每每召开新闻发布会，总会引动中外媒体的关注。除了这些，还常撰文，或阐述治国理念，如《关于社会主义初级阶段的历史任务和我国对外政策的几个问题》，或纪念恩师，如《再回兴义忆耀邦》。要理论有理论，要思想有思想。胡锦涛何及其万一？ <br />
<br />
　　再说能力。胡锦涛学理科的，却跑去搞学生工作，再进一步成为团干，除了一门心思往上爬，讨好上司换得仕途升迁，还会什么？中国百姓比较朴实，但凡能干一点的官，百姓总是能记住他们的好，比如“要吃粮，找紫阳”，“要吃米，找万里”。大凡中共领导人上台，媒体也会连篇累牍地宣传他们以往的政绩，但令中宣部伤透脑筋的是，挖地三尺也找不出胡锦涛在甘、藏、黔这些地方的丝毫政绩。面对国内贪污腐败，贫富差距，环境污染，民族矛盾，道德沦丧，上学难，住房难，就医难等等，没见他出过一个策，划过一个谋。这样的人贵为总书记，哪里是万民之幸! <br />
<br />
　　温总理早年地质专业毕业，一来甘肃就走遍山山水水，在向中央领导汇报时对甘肃地质情况如数家珍。及至副总理，主管农业，走遍中国大部分农村，98抗洪，功劳是江泽民的，但苦劳却实实在在是温家宝的。及至登顶宰相，仍下矿井，走农村。免农业税，一改中国数千年之传统，救农民于水火。抗危机，四万亿大手笔，使中国经济独秀于世界。八年前，中国尚四处作揖，八年后，列强纷纷向中国示好。中国经济实力令世界刮目，温家宝之功当数第一。 <br />
<br />
　　就连所谓的亲民，也完全衬出胡与温的高下不同。大凡重要的举动，都是温总率先而为，而胡锦涛东施效颦。先行者是真亲民，后学者是假做秀。十六大胡温上台伊始，即有新政之声。现在看来，新则新矣，只在温总。亲民是温总先做出的，胡锦涛步其后。抗震救灾，温总两次都是第一个到达现场，胡锦涛也总是落后。最能说明这一点的，就是“粉笔字秀”：2008年5月23日，温总理视察劫后余生的北川中学临时教学点时,在黑板上写下了“多难兴邦”这四个字，一时传为佳话，而2010年4月18日在震后玉树胡锦涛效法温家宝，写下“新校园，会有的！新家园，会有的！”这12个粉笔字。若没有中宣部开动宣传机器，胡锦涛的狗尾续貂，不会成为所谓的“佳话”。温总理写粉笔字在前，那是真性情真亲民，胡锦涛学粉笔字在后，那是假亲民真做秀。 <br />
<br />
　　胡锦涛如此拾人牙慧，只能说明他的智力有问题。智力有问题不要紧，一个领导者，不必事事比人强，更不必样样都能懂，会用人能用人用好人就可以了。但胡温这些差距，正说明在用人问题上，胡更不能识人用人。 <br />
<br />
　　且不说治国方略了，就连“亲民秀”，胡身边那些那些总管、秘书、化妆师，比如中央书记处书记、中央政策研究室主任王沪宁，中央书记处书记、中央办公厅主任令计划，中共中央总书记办公室、国家主席办公厅主任陈世炬等等，个个电视上正襟危坐，人模人样，出国陪同有他们，国宴山珍海味有他们，外交迎来送往有他们，升官发财更有他们，但该他们给胡锦涛出个像样的主意时，就没他们了，就是不能给胡出哪怕一个好主意。出不了好主意，也不要紧，别亦步亦趋学人家温家宝啊。温总是真亲民，你学就成了做秀。一开始他们学温总还换个花样，温总下个矿井，他们赶紧给胡锦涛戴个安全帽；温总吃个馒头，他们就让胡锦涛赶紧去包个饺子。最后竟然连花样也懒得变了，温总写个粉笔字，就让胡锦涛也写个粉笔字，直接玩盗版。哪怕是你改改毛笔呢，也算有点了“创新”啊。 <br />
<br />
　　怪不得中国近年来兴“山寨文化”大行其道呢，胡锦涛直接盗版温总的粉笔字秀，说明“山寨文化”的源头，就在中南海哩。 <br />
<br />
　　真是兵熊一个，将熊熊一窝。胡锦涛照着自己的模子识人用人，用的全是比他还弱的人。中国历史一再印证，这些人除了当太监，伺候主子吃喝拉撒睡外，政事上绝不能指：坏事全有他们，正事没一个能行。东汉末年的十常侍，明朝末年的阉竖专权，历朝历代衰亡曲中，这些人都是最为重要的休止符。 <br />
<br />
　　相反，温总身边的人，个个低调，人人能干。比如国务院秘书长马凯，曾当过国家发改委主任，对中国经济问题了如指掌。总理办公室主任丘小雄，是北大经济系的高材生，正儿八经懂经济搞经济的。根本是胡身边那些动笔杆子，耍嘴皮子，或者专事伺候主子的奴才所没法比的。 <br />
<br />
　　总之温总理在副总理时期的任劳任怨，八年总理任期内的鞠躬尽瘁，充分展示了一个大国总理的才能和胸襟。不光国人称赞，就连国际社会也赞誉有加：2008年温总被评成为美国知名网站 Facebook最受欢迎政治名人之一。2010年年初，美国著名智库欧亚集团评选出2010年最值得关注的全球十大领导人，温家宝排名第一。而胡锦涛呢，4月份的一项全球最受欢迎领导人国际调查中，胡锦涛惨遭垫底，排名甚至在利比亚领导人卡扎非之后。 <br />
<br />
　　有不喜欢温家宝的左派认为，胡锦涛这个人厚道，作为党的总书记，开好船的责任太大，为了让大家同舟共济，只能是隐忍个人理想，以大局为重。而温家宝这个人是政坛老手，惯于翻手云雨，过去背叛赵紫阳而投向邓小平，现在又背离胡锦涛而投向江泽民，什么时候都是两头不吃亏，而且只知道做秀，没有真才实学。等等。 <br />
<br />
　　这说种话的左派，他们不是不明白，船行何方，不是由大副决定的，而完全是由船长决定的。身为中国这条大船的船长，在世界文明发展波澜壮阔的今天，不进则退，胡锦涛有什么可资称道的作为？他一路上小心翼翼避开的，不是会导致中华民族危亡的暗礁、险滩，而完全是可能会给他自身带来潜在危险的激流、峡口。决定他行为的，不是中华民族的利益，而只是个人安全进退。纵然是一跃而使中华民族百病得愈的机会，只要危及到他自身，他都决不会前进半步。最能说明这一点的，就是政治改革。经20余年经济改革，至胡锦涛手上，已积累了丰厚的政治改革资源，只要下定决心，就能冲破险阻，缔造中华民族新纪元。但胡为了不让别人说他是戈尔巴乔夫，不仅对政改避之未恐不及，而且祭毛魂，展左旗，大兴文字狱。不仅右派刘晓波莫名入狱，就连左派张宏良的博客也被封杀，一时间中华大地复现莫谈国是的清末旧状。 <br />
<br />
　　若温家宝是总书记，这种情形可能会出现吗？对于悲天悯人，而常怀救黎民于水火之心，数度表达政改追求的温家宝，左派们除了棍子，还是棍子。 <br />
<br />
　　左派之所以总是贬温扬胡，不是因为胡做的好，而是左派权斗的立场手法所致。因为历史上，左派之所以永远不倒大霉，是因为他们永远骂老二、老三和下台的老大，不骂台上老大。文革时，他们骂刘、周、邓，但绝不骂毛泽东。邓小平时期，他们骂胡耀邦，骂赵紫阳，但绝不骂邓小平。江泽民时代，他们骂邓小平、李鹏、朱F基，绝不骂江泽民。现在胡当政，他们当然只骂温家宝，顺带骂骂江泽民而已。这是左派金身不倒的秘技。这里面的代表人物，如邓立群，再如90年代写万言书的那些左派，再如何新，再如现在的张勤德、张宏良。 <br />
<br />
　　最近左派又掀起一股借古喻今的批判之风，以批“宰相”为名苛责温家宝总理。对于左派而言，纵然台上是阿斗，他们也只会骂诸葛亮，而不会骂阿斗。左派们搜罗了一些历朝历代灭亡时宰相的作为，以证明“千古兴亡，亡于一相”。孰不知，这些左派们根本不懂历史。中国历史上，并非永远都是皇权与相权的结构。明朝相权是被剥夺最厉害的。清朝的相权也不大。一个不大的相权，能有何作为？左派们为什么不再进一步思考一下，每个朝代灭亡时，不都有一个把祖宗江山给弄丢的皇帝吗？左派们为什么不批评批评皇帝呢？同样，历史上不是有那么多中兴盛世，都有一个为人称道的宰相吗？左派们为什么不拿这些有为的宰相来比照一下自己的观点呢？ <br />
<br />
　　对于左派而言，假若温家宝是总书记，他们还敢骂吗？ <br />
<br />
　　对于中国而言，假若温家宝是总书记，中国进步的是不是更快？ <br />
<br />
　　历史没有假若，所以现实只有伤感。</div>

							
							
							
							
