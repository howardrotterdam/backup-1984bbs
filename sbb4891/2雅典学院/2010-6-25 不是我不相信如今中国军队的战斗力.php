													<h2>不是我不相信如今中国军队的战斗力</h2>
						
						
																											<div id="postmessage_703164" class="t_msgfont">今天早上比较闲，和朋友聊起军队，就整理出来吧。<br />
<br />
我身边当兵的朋友特别多，因为我老家太穷，当兵是比较好的出路，回来一般都安排工作。<br />
<br />
一个哥们在青海政治监狱当狱警（也不知道该不该叫狱警，反正是监狱里管犯人的），鸟不拉屎的地方，他们都不担心有犯人跑路，因为完全是荒漠戈壁，跑出去也没地方躲。而且那地方类似秦城监狱，关的大多是贪官啥的，这群人脑满肠肥的，也跑不动。平时这群人最大的娱乐项目就是打磨石子，据说有人在里面刻骨钻研，都能搞出艺术品了。<br />
<br />
在那地方曾经出过这么一个事，有一天点名，发现少了一个狱警。一去宿舍看，留了个纸条，跑回家了，原因是他去当兵了，他爸在家里被村委会的一群官员和恶霸强占屋子，把他爸打得遍体鳞伤，他忍不下这口气，跟上头请假又不批准，于是一冲动，就逃学回家，给他爸报仇去了。这个问题严重之处在于，他是带着枪去的，还带着子弹。这下闹大了，这可是大事啊，于是一路上围追堵截，结果在快到老家的一个火车站截住了，他一定要回家，又是个爆脾气，截他的武警又不懂啥叫谈判，结果几句不对两边就有开枪擦火的意思，最后在火车站当场击毙。<br />
<br />
这个事情本来是个悲剧，但是我觉得这个悲剧完全可以避免的。<br />
<br />
1 请假，这种事请假完全没有问题，但他没塞钱给他队长，一农村孩子单纯得要命，懂啥行贿受贿啊。队长看这小子这么不通人情世故，就想治治他，所以请假不批<br />
<br />
2 枪械，这玩意在中国是管制的，军队里理应管理得更严，每天都会收枪入库盘点，但他居然能带着枪从青海走到贵州。注意还有一点，他们是用长枪执勤，但他最后是带着手枪出去的，他在哪弄的？就这么一个不通人情的农村娃都能在军库里弄到手枪，如今管理的混乱程度可见一斑。<br />
<br />
接着再说我这个狱警朋友。他们周末的一项固定工作，就是去给上级领导在市区的家里搬东西，监督装修工，打扫卫生之类。这可是个好活，大家都抢着干呢，因为能去市区，而且再怎么也比监狱好玩啊。其实那领导也是个小领导，但这房可是他买的不知道第几套了，钱怎么来的呢？大家为了升士官塞的钱嘛。我的朋友最后因为塞钱没到位，升不了士官，退伍回来了。<br />
<br />
另外一个在西南某地当炮兵的朋友，跟领导关系好，经常晚上一起出去嫖，搞到第二天白天，就不出操，白天睡觉晚上出去鬼混。上次听他说，半年没怎么摸过炮了。这人很有头脑，跟领导关系混得不错，所以领导后来索性直接调他到小车部，给领导专职开车。别小看这小车部，几乎是军队里最吊的部门了，任何集体活动都可以不参加，想什么时候开车出去就出去，想什么时候回营就回营，整个宿舍里满地的烟头，上面要问，就说没时间整理，跟领导出任务去了。结果后来被一个领导的领导心血来潮来抽查，才打扫了一下，维持了一段时间，结果慢慢的又这样了。<br />
<br />
还有一个做空军地勤的朋友，他到部队之后领导给的第一个任务，是给他一个表格，上面清晰的写明了每个菜地菜熟的时间，让他和另外几个人24小时轮流帮他偷菜。领导是个大烟枪（好像部队里没人不抽烟），每次去飞机里做例行检查一群人都抽着烟，而且因为空军地勤很多有关系人士的子女都往这里来，所以这群人很随便，队长也不敢管，怕得罪他们将来影响自己仕途。所以好几次我看他手机上网，问他在哪，说，正在修飞机呢。<br />
<br />
上次有个新闻说飞机栽了，我问他知道不，他说这不就我们基地的么，栽飞机算啥，平均三个月一架。<br />
<br />
<br />
还有一个朋友，在南部沿海城市当海军。到了复员的时候竞争很激烈，大家都塞钱，他是拼了老命想留部队里，家里准备了20来万，最后还是没成？为啥？因为别人送得更多。其实他们一个月工资不高啊，1000多点，这20万怎么回本？很简单，他所在的机构负责采购，一台5000的电脑按20000出单，然后整个办公室里按照职位高低来分成。20万？没多少时间就赚回来了。最后他没成，走的时候把办公室里的哈苏相机带走了。我也不知道为啥他们有哈苏相机，他自己也不知道……<br />
<br />
我有一次和某个军分区的政委吃饭，酒过三巡之后胡扯，谈到说，再来一次64你去不？他说去个屁，当年去的发奖的拿勋章的现在提都不提了，万一以后清算怎么办。接着他又说，如果你说现在越南之类别的国家打过来，我还会带队去打，如果是云南打到贵州来，呵呵，这个嘛。。<br />
<br />
所以我现在对中国军队的所谓战斗力是嗤之以鼻的。大部分人当兵就是为了好找工作，有多少是怀着保家卫国的热情去的？就算是这样，到了部队发现老兵欺负新兵，发现做什么都得靠关系，发现自己就像旧社会时候地主养的奴仆一样要为领导服务，发现上位不是靠实力而是靠塞钱，这群人还有保卫国家的使命感荣誉感？反正在我身边，从未感觉到。我不想以偏概全，不过事实往往更加残酷。</div>

							
							
							
							
