													<h2>美国之音：温家宝没时间了 CNN谈政改像“政治遗嘱</h2>
						
						
																											<div id="postmessage_857134" class="t_msgfont">中国总理温家宝在9月份出席联合国大会期间接受美国有线电视新闻网CNN的专访。在回答一系列国际与国内重大问题时，温家宝再提中国政改。<br />
<br />
&nbsp; &nbsp; 在CNN10月3日星期天播出的访谈节目中，主持人法瑞德.扎卡利亚说，最近一段时间以来温家宝总理多次提及政治改革，而他在中国认识的很多中国人表示，他们喜欢温总理的讲话，但是没有看到政治改革的行动。<br />
<br />
&nbsp; &nbsp; 温家宝回答说，“实际上，政治改革是邓小平多年前提出的。我想，任何一个对国家有责任感的人都会对这个问题进行深入思考并付诸行动。”<br />
<br />
&nbsp; &nbsp; 温家宝说：“我的观点是一个政党在取得政权成为执政党后，应该跟在夺取政权的过程中不一样，最大的区别应当是，这个政党的行动应当符合宪法和法律的规定。政党的政策和主张可以通过合法程序体现在宪法和法律之中。任何党派、组织和个人都必须遵守宪法和法律，没有超越的特权，必须以宪法和法律为行为准则。”<br />
<br />
&nbsp; &nbsp; 温家宝将他的政治理念总结成四句话：“让人有尊严地过幸福生活，让人感到安全可靠，让社会充满正义，让人对未来充满信心。”<br />
<br />
&nbsp; &nbsp; 比起温家宝在近来40多天来多次谈政改的任何一次，他这次与CNN谈的都要更加具体，不是一句话笼统带过，而是谈到执政党要在宪法与法律框架内执政的问题。<br />
<br />
&nbsp; &nbsp; 中国宪政学者章立凡认为，温家宝提出这一点很有意义。他说，中共自80年代末期以来，很多行为超越宪法，比如党大于法、党委干预司法、对人民各种宪法规定的自由权力的限制，频频制造侵犯人民利益的事件。<br />
<br />
&nbsp; &nbsp; 章立凡说，温家宝提出的执政党在宪法与法律框架内执政的政治体制改革的主张是1987年中共13大提出来的。他说，现在回归13大路线恐怕是挽救中共执政地位的唯一有可能有效的途径，但会遭遇巨大阻力。<br />
<br />
&nbsp; &nbsp; 他说：“虽然执政党的领导人有这样的见识，认为是回归宪政的时候了，但是可能会遭遇巨大的阻力，我们看到20多年过去了，时间都已经耽误了，各种利益架构都已经形成了。”<br />
<br />
&nbsp; &nbsp; 温家宝总理在CNN专访中还回答了对中共前领导人胡耀邦的历史评价问题。温家宝首先肯定，胡耀邦是一位非常好的领导人，他认为他给胡耀邦的历史做出了一个公允的评价。<br />
<br />
&nbsp; &nbsp; 温家宝说：“他为中国的改革开放做出了贡献。第一，他大力推动有关真理标准的辩论，通过辩论，他为思想解放做出了重大贡献。第二，面对众多阻力，他毫不畏惧，为了文革中遭受迫害的大量党政干部平反冤假错案。第三，他采取切实行动推动中国的改革开放。”<br />
<br />
&nbsp; &nbsp; 宪政学者章立凡认为，胡耀邦是中共目前仅存的优良政治资产之一，现在中共需要盘活这一所剩无几的优良政治资产。他相信，在未来某一历史时刻，另一位中共前总书记赵紫阳也会得到正确的历史评价。<br />
<br />
&nbsp; &nbsp; 温家宝在与CNN访谈中有关言论自由的讲话受到广泛关注。温家宝说，“我认为言论自由对每一个国家，不论是发展中国家还是发达国家，都是必不可少的。中国宪法保障言论自由。”他说：“不但要让人民享有言论自由，更重要的是，要创造条件让他们能够对政府的工作提出批评”。<br />
<br />
&nbsp; &nbsp; 与此同时，温家宝说，对政府的批评要在宪法和法律允许的范围内。他强调了秩序的重要性：“对一个有着13亿人口的国家来说，秩序更为重要。<br />
<br />
&nbsp; &nbsp; 还有两年，温家宝将卸任总理职，交出接力棒。在与CNN访谈中，他表示对中国新一代领导人有信心。他说：“第一，正如中国老话所说，‘长江后浪推前浪，一代新人换旧人’，我有信心，中国未来领导人将超越他们的前任。第二，人民和人民的力量决定国家和历史的未来，人民的意愿和愿望不可阻挡，顺之者昌，逆之者亡。”<br />
<br />
&nbsp; &nbsp; 温家宝总理与CNN 的访谈被广泛评论为听上去更像政治遗嘱，是对历史的一个交代。也有人评论说，温家宝是在作又一场政治秀，没有任何实质行动。<br />
<br />
&nbsp; &nbsp; 不过宪政学者章立凡表示，他相信温家宝的诚意：“他能够明确他的政治理念，能够完整地把它提出来，总比不提，甚至抵制，甚至在他上次讲话以后，某些势力已经发动了新一轮对他的批判(要好)。我觉得相对来讲，我还是要支持他的。”<br />
<br />
&nbsp; &nbsp; 舆论普遍认为，如果温家宝不是在作秀，而是将其政治理念付诸行动，发起政治改革，也不可能在最后的两年任期里完成这一重大的历史使命。然而，中共前总书记赵紫阳的政治秘书鲍彤敦促温家宝在未来的两年时间里不能无所作为。他说：“两年是很宝贵的，可以做很多大事情。”<br />
<br />
&nbsp; &nbsp; 温家宝总理在与CNN的访谈中还谈到中国的经济政策、人民币汇率以及双边贸易等一系列美中关心的议题。</div>

							
							
							
							
