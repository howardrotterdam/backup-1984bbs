						<h1>你如何看待现行的计划生育政策？</h1>
			<div class="postmessage">
								<div id="postmessage_304300">首先来阐明我的观点：<br />
白岩松27号在《新闻1+1》中说：<br />
第一个问题，计划生育是国策，但不是铁板一块，一定会随着人口年龄格局的变化而变化，那么什么时候变呢？是宁可早一点变还是晚一点变，这是第一个问题。<br />
第二个问题，应该允许社会民间的资本进入养老，在国家确定了是国家战略和大的政策方向下，中国的事政府一重视就好办，但是在各种资本进入的时候，我们也是否可以宗教的、团体的这方面的能力，也可以在这个领域发挥重要的作用，这是第二个。<br />
第三个，我更主张的是不增加床位的养老模式的尽早建立，在城市的叫社区模式，在农村的像刚才大姐讲的，当地的农村妇女，她正好解决不了劳动力的问题，但是用市场的行为，购买服务，国家给予一定的补贴，那么这些人在城市里，四零、五零人员就进入到了社区养老，而且不增加床位，还在你们家住着，但是我给你一天提供服务，送餐等等。<br />
白岩松在节目里说这番话，可以认为他是满嘴跑火车，但是不是有部门用来测试民意的气球？――央视论坛稍早前的投票显示，逾七成网友支持取消现行的计划生育制度。<br />
从《新闻1+1》提供的数据可以知道，中国超过60岁的老人占总人口的12.79%，也就是1.62亿60岁以上的老人，其中已经失去生活自理能力的老人940万，还有部分不能自理的老人近1900万。全国现有的福利机构只能解决108万的老人的养老问题，杯水车薪，而且在可见的将来，也无力解决。<br />
<br />
现在一提起调整计划生育，就容易条件反射――“放开了生，这还了得”？实际上很多学者探讨的重点是“调整”，而非“取消”，简言之是允许二胎（且不局限于双方独生子女和农村人口），这种观点可以说拥有强大的民意，不信可以去看任何一个网站有关这个议题的讨论和投票。但政府不允许媒体放开讨论，实属不智。<br />
坚持计划生育的观点认为，人口爆炸本来就是个错误，通过增加生育来解决老龄化，不是好办法。如果一放开，中国人口到达15亿甚至20亿怎么办？资源是有限的，中国没有这么多资源来养活这么多人口。对相当一部分人来说，更现实的问题是不敢生，因为养不起――房价、教育、医疗等基本生活保障的压力都很大，没有这个能力多生。所以，痛苦是这一代人必须承受的。<br />
支持计划生育也有民意支撑，其中不乏名人：何新、汪晖、何清涟、李银河……政治光谱从左到右。<br />
反对的人说，现行计生的政策，已经显现出严重的社会问题。独生子女对于中国各种传统文化的继承、团队合作精神、人际之间的关爱等，都有较大的欠缺。更多的人则觉得只生一个孩子真的太少，独生子女长大后工作、参军或有什么意外――去年的汶川地震已经突显了这种悲剧，以及为了养老承受着前辈人所没有的压力。80后都已经快30岁了，二三十年后也将失去劳动力，再不改就来不及了。<br />
希望调整计划生育的名单可以列一大串：茅于轼、吴敬琏、朗咸平、张维迎、贺卫方、何亚福、仲大军、陆学艺、胡鞍钢、滕彪、杨支柱、孙立平、林毅夫、孔庆东、穆光宗、李昌平、高清辉、薛涌、韩寒、曹锦清……政治光谱、专业方向和学术主张更是五花八门，易富贤和杨支柱走得更远，要求马上取消，认为生三胎才是主流，如果夫妻双方愿意，四五胎也可以考虑。<br />
最有力的反对声音来自另一个人口大国――印度，1998年诺贝尔经济学奖得主、印度学者阿马蒂亚森通过比较中印两国的人均GDP，也得出相同的结论。计划生育无功于当代，却有害于千秋。<br />
<br />
公说公有理，婆说婆有理。那么，再回到数字――<br />
对照五次全国人口普查的数据，1964年0-14岁少儿占比为40.69%，1982年、1990年和2000年的这一比例分别降至33.59%、 27.69%和22.89%，到2007年这一比例已经降到17.88%。尽管近几年中国的计划生育政策已经略有放宽，但少儿人口比例一直在大幅下滑，这可能是许多70和80后独生子女在就业难、高房价和高教育费用的压力下推迟结婚和生育（甚至出现了许多丁克族）导致的。这是一个十分危险的信号，意味着出生于70和80后的独生子女在未来将面临更严重的老龄化挑战。<br />
计划生育政策执行三十多年来，从“该扎不扎、房倒屋塌，该流不流，扒房牵牛”的宣传口号到强制人流、母子双亡、激化社会矛盾的悲剧，其中的惨烈情形，至今仍然是媒体报道的禁区，甚至有蛇头专门伪造因超生被乡镇干部迫害的图文资料，用来做偷渡获得国外居留权的生意。<br />
生儿育女，本来应该是吃饭拉屎放屁一样的天赋人权，生多生少是每个成年人的自我选择。当初中国出于长远利益考虑，强制推行计划生育三十余年，到今天各种社会问题确实已经显现，既然有人提出“调整”，更多的人希望能够生二胎，那么，不妨在上全国两会上认真讨论一下，让媒体报道、征集民意，又有什么不可以的呢？<br />
