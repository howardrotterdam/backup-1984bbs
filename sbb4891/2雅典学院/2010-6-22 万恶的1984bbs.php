让建房成为教学过程<br />
<br />
 <br />
<br />
　　笔者初见谢英俊，是在乡建学院2005年暑期营。他与来自清华大学等二十所大学建筑专业的四十名本科生、硕士生及博士生一起，自己动手盖一号二号呢。那时地球屋一号的土墙已经添到了一半，土墙上残存的麦粒发了芽，远看像艺术品。我去的时候恰逢二号上梁，谢英俊正在指挥人将绳子拴在一号楼顶的架子上，然后要借它的力量把二号楼原本躺在地上的轻钢框架拉起来。<br />
　　我觉得有点儿玄，一号就像是搭积木搭起来的架子，拉散了怎么办？但谢英俊却不当回事，笑眯眯的：“那就试验一下好了。”<br />
　　进度实在太慢了，一会儿绳子断了，一会儿固定绳子用的木棍又不合适，一点问题都要处理半天，虽然谢英俊还是不急不慢的样子，我等不及了，就去了前面的办公室。后来听到噼里啪啦鞭炮响，是上梁的鞭炮，原来二号的框架已经立了起来，再扭头看看一号，还在呢。<br />
　　过一会我到食堂，正好和他一桌。坐下之后，才听清楚这张饭桌上的话题竟是厕所，而且边吃边谈的居然是厕所的味道和里面的小虫虫――蛆。学生们谈到清理厕所时看到有蛆，按设计不会有蛆虫的。谢英俊说，这是因为用毕没有盖好，只要用过后立即盖好，苍蝇没有机会接触到粪便，自然就不会有蛆了，一边说一边还用手示意着。<br />
　　工作营四十几名成员大部分是独生子女，平时连家务也极少做，而在工作营里，必须轮流值日帮厨、收拾垃圾和清理厕所。谢英俊认为现在的教育在很大程度上脱离生活，学生应该首先是生活中的人，甚至清理厕所，那里都是我们自己的排泻，应该学会面对它。<br />
　　在这里，不论男生女生，一律是体力劳动者，这些从没有动手盖过房子的小知识分子必须自己动手，从夯土和泥打地基一点点做起，用自己的双手建房子，尝试怎么用扳手螺帽构建框架，怎么用绞链绳索把框架立起来。谢英俊之所以要让学生们自己动手盖房子，也是对目前建筑教育一种“反拨”。<br />
　　即使是在中国，人们更多地谈论和关注的，是那些世界著名的都市建筑，而在这里，谢英俊带同学们一起，用电脑演示中国传统的用麦秸和泥土“浇铸”的墙体，探讨这些与水泥建筑厚度相仿的墙为什么会冬暖夏凉，还有传统木作不用铁钉而是用燕尾榫联接的原理，以及古老的火炕的优缺点，引导他们一点点发现中式建筑的美感和实用性，探讨在现代条件下如何运用。<br />
　　建筑营同学最初都很不习惯，因为谢英俊对于学生们的问题不是有问必答，他更多的是反问，会一步步用问题引导同学们思考并自己得出答案。谢英俊说每建一座房子，都是一个培训班。营里的同学说能够在谢老师的指导下工作是一种幸运，学到课本上学不到的技能，更重要的是从他那里学到了许多治学、做人的道理，是幸福的。<br />
　　谢英俊在建筑专业学生中举办这个工作营，还有着他自己的用意：要帮助更多的青年学子迈进这个“门槛”，才能够更有利于协力造屋理念在农村的推广。<br />
　　建在乡建学院的示范房既有时髦的室内浴室，也有传统的火炕。“一号”敦实稳重，样子比较传统；“二号”坡屋顶老虎窗，洋气一些。两栋房子外墙都抹上了厚厚的石灰，不用担心透水，白墙亮瓦，不管哪一栋，看上去都很气派，全没有老辈子人记忆里“泥巴房子”的土气样子。<br />
　　谢英俊对村民说，在欧洲，这样的房子是最贵的。村民们进去看一看，确实不错，绝对不会影响给儿子说媳妇。<br />
　　乡亲们是最重实际的人，都在心里算了一笔账：学院盖这房用了五万块，要是自家盖的话，麦秸和土都不用花钱去买，用拆旧房拆出来的材料就有了打地基用的砖石。学院雇工都是要花钱的，自己盖房可以请亲戚朋友来帮忙，这笔钱还可以省下。这么一算，两三万就能把房子盖起来。看来真的像这个台湾人说的，只要我们有劳动力又有劳动的愿望，就可以每家人都住上漂亮的欧式小楼了。<br />
　　谢英俊对在大陆推广合作建房胸有成竹：“相比市场上的建筑队，农民协力造屋工作队完全可以用三分之二的报价建成房子。而且，我们的品质他们做不到，在市场上有竞争力。只要肯干，我们有信心在大陆农村全面推广。”<br />
