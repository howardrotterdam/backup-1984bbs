不只是盖房子<br />
<br />
 <br />
<br />
　　当然，并非事事尽如人意。邵族长期被压制被排斥，贫穷不仅改变了他们的生活方式，也侵蚀着人的心灵，很多人养成了酗酒、懒散、依赖救济的习惯。新房落成不久，有人向谢英俊反映：我的前厅漏水了。谢英俊去看过，漏水并不严重，他们连房子都盖得起来，这点问题自己动手完全可以修好，他们似乎觉得盖房子、修房子都应该是谢英俊他们一帮人的事。他们自己不修，谢英俊也不修，族人是出于依赖的惯性，谢英俊则在试验，看多长时间他们能够自己动手，会发生改变，一直等到两年以后，他们才自己修好了漏水的前厅。<br />
　　说到这，谢英俊依然是不急不慢：“一开始我蛮着急的，后来知道不只是在盖房子，是在做人的工作，做这种事情急是没有用的，只能慢慢来。”<br />
　　邵族人最初接受谢英俊，其实是因为他们穷困潦倒，没有其他的选择，这一点让他痛心，也带着一种“欣慰”：不管是出于什么原因，他们毕竟接受了自己的房子。连谢英俊自己都说，如果他们有钱，一定会把他为他们盖房子拆掉重盖，所以才“约法三章”，规定不得更动或者破坏房屋外观。后来，有来日月潭旅游的人慢慢注意到这些房子，赞美“另类”、“自然”、“标新立异”、“与自然融为一体”等等，原住民自己也开始换一种眼光看待这些房子，会自豪地说“这就是我们邵族的房子”，“这就是我们泰雅族的房子”。<br />
　　“9・21”大地震后，许多团体个人积极加入了灾后重建的工作，第三建筑工作室是唯一一家将地震灾民吸收到重建工程中的施工单位。灾后三年，仍然从事重建工作的就只剩了第三建筑工作室一家。他们还参与了争取土地和权利的斗争。至今距地震发生已有七年，他们的工作还在继续。<br />
　　谢英俊和他的团队以其作品“9・21家屋重建：邵族安置社区”获“第三届远东杰出建筑师设计佳作奖”，他也成了岛内一时争说的对象。正如媒体所言：“建筑的外行人将谢英俊视为善心人士；媒体为他造神话；部分台湾的建筑专业者认为他的建筑简陋而美学价值不值一提；国际建筑界惊叹他的建筑作为。”不管怎么说，“谢英俊起身对抗整个体制的状态，使得他的建筑超越一般美学的领域，进入更深的文化意涵。”<br />
　　当部落重建成为常态，由于工作队是用常规建筑商报价的四分之一左右完成工程，已经能够在家屋重建过程中实现自偿，他们在原住民地区建造了三百余套房屋，运转良好。谢英俊这样做，实际上打破了现有的建筑设计、发包承建及监理制度，将建筑权从专业厂商手中夺回到灾民手中，也就是减少了建筑业利润。谢英俊之所以被业内诟病，原因盖出于此。<br />
　　协力造屋的尝试在国际上早已有之。1907年，德国的基督教传教士古斯塔夫・冯・保德史威瀛（Gustav von Bodelschwingh）创立“家园协会”，与贫病交加的雪茄工人和农民一起，用自己的双手，在橡树、山毛榉、杉木林间建设家园。前苏联切尔诺贝利核电站发生泄漏事故后，同家族的迪特里希・冯・保德史威瀛（Dietrich von Bodelschwingh）牧师在德国创建了“要家园不要切尔诺贝利协会”。1991年，一群德国志愿者与核污染灾民一起，建造起三十三座就地取材的“粘土木架屋”，十年内，吸引了一千人参与其中，每年夏天捐出三周假期与灾民一起重建家园。<br />
　　谢英俊一再强调，建筑是一个专业性极强的行业，即使是灾后重建，第三建筑工作室大量使用了轻钢结构，所用市场上的轻钢接口都是昂贵的专利产品，这些专利技术是被大公司垄断，第三建筑工作室之所以能够大幅度降低成本，是因为有自己的接口技术，安全、简单，没有施工经验的人一学就会操作。这一点是最让谢英俊引为骄傲的：“技术问题恰恰是需要我们建筑师有所作为的地方，如何让它更开放，没有专利，让更多人参与。让手无缚鸡之力的书生都能盖房子，那才是我们建筑师的本事。”<br />
