													<h2>熊培云：不要活在新闻里</h2>
						
						
																											<div id="postmessage_699015" class="t_msgfont">2010年05月22日 14:48新京报【大 中 小】 【打印】 共有评论3条<br />
行于思想国<br />
<br />
“新闻不是生活的全部，而且新闻是免不了要说人性的坏话的。就像我们现在通常说的“要在生活中维权，不要在维权中生活”，那好也不要活在新闻里。”<br />
<br />
在开始这个题目前，不妨想想人类历史。你一定会轻而易举地想到许多黑暗的场景：冲突、流血、暴动、自杀，甚至屠杀……公平理所当然，不公正的事却长久萦绕于心，时而让你寝食难安。难怪人本主义心理学家马斯洛先生当年会这样感慨：所谓人类历史，不过是一个写满人性坏话的记事本。而事实上，这世界不只有血流成河，不只有征战杀戮。无论世事怎样变迁，阳光依旧在温暖我们的身骨，草木应季生长，人们生而育女。<br />
<br />
那么新闻呢？只要稍加留意，就会发现新闻一样在不断说着人性的坏话：某位官员腐败了；某地又发生了屠杀婴幼的惨案，而此前其他地方的相同罪恶早已令人发指；某地有人自杀了，而且是几连跳；某地有人自焚了，有人推倒他家的房屋；某地有人在政府门前集体下跪了，为了解决问题；某人遭遇了巨大的不公正，而你对此无能为力……<br />
<br />
天天都有这样的新闻，你难免会心生绝望：这世界、我所处的时代就是这样的吗？当然，这样的绝望时间通常不会很长，常常转瞬即逝，至少我是这样。因为我知道，新闻不是生活的全部，而且新闻是免不了要说人性的坏话的。就像我们现在通常说的“要在生活中维权，不要在维权中生活”，那好也不要活在新闻里。<br />
<br />
这实际上关系到一个媒介素养的问题。所谓媒介素养，说到底就是受众接受、解读媒介信息的一种能力。<br />
<br />
就个人而言，如果能积极独立地思考，通常都不会天真地以为新闻里的“坏人坏事”，就是我们的生活全部，并由此得出“形势一片大坏”的结论，正如当年“形式一片大好”的宣传，不能遮蔽现实的困境一样。就新闻事件本身而言，好也罢，坏也罢，都只是对生活的取景，对现实的断章取义。<br />
<br />
我们应该具有的媒介素养是，负面新闻并不构成对其工作的全盘否定，更不意味着媒体包藏恶意。一个人在生活上有洁癖，人们多会同情之理解，毕竟，那也算是个人自治的一部分，其他人无权干涉；但如果带着洁癖去看新闻，去搞管理，难免脱离实际，而且显得公私不分。生活不是童话，童话里也有大灰狼。必须承认的是，媒体报道“说人性坏话的新闻”，除了商业上的考虑，还因为媒体肩负了一种责任，即社会需要通过它了解社会运行是否正常。<br />
<br />
最近自杀案与恶性杀人事件频繁出现，人们在讨论一个问题，即媒体报道是否起了推波助澜的作用。在这方面，有些媒体着实需要省思。在过去的报道中，我的确看到许多媒体总是不厌其烦地描述杀人过程与杀人现场，唯恐读者不能身临其境。另一方面，过度批评媒体的负面报道，难免有苛责之嫌。<br />
<br />
作为平衡，其实重要的是改变受众的观念：一方面，需要提高受众的媒介素养―――媒体报道远处的一场火灾，你却因此烧毁自家的房屋，不理智的显然不是媒体，是纵火者；另一方面，全社会更要直面已然发生的不幸事件，以求彻底改造，而不是头痛医头，脚痛医脚。幼儿园不安全，要加强保安，更要消除“深层次原因”；有人自杀了，更要查找究竟是什么导致其走上绝路。更严重的是，如果一个工厂接二连三发生跳楼事件，这已经不是个体的绝望，而是集体性的绝望。<br />
<br />
一个理性的社会，应该给那些潜在的自杀者，以继续活下去的希望，而不是前仆后继的勇气。说一个人以死“唤醒社会”，实在是轻贱了人的生命，因为谁都应该好好活着；如果这种死不能让社会了解背后更实在的原因，而停留于指责是媒体起了坏作用，并大加讨伐，实在是用错了力。<br />
<br />
因为这些事情的发生，最近许多人都在讨论“维特效应”。据说歌德的小说《少年维特之烦恼》发表后，造成了极大地轰动，不但使歌德名声在欧洲大噪，而且在整个欧洲引发了模仿维特自杀的风潮。说“风潮”，显然言过其实。事实上，《少年维特之烦恼》也让很多人活了下来，消解了内心的忧愁，其中包括歌德本人。<br />
