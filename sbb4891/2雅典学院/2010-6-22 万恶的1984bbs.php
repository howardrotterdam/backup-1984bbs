													<h2>安得广厦千万间</h2>
						
						
																											<div id="postmessage_698009" class="t_msgfont">转一篇读库的文章，我很喜欢的：<br />
谢英俊：安得广厦千万间（原载《读库0705》）<br />
寇延丁<br />
<br />
　　2006年3月18日，在河南省兰考县葡萄架乡贺村，来自全国各地的三十一个农民与学生志愿者，以及贺村合作社生态建筑队的十四名队员，一同参加兰考春季生态建筑工作营培训。谢英俊的八亿农民合作建房理想，迈出了第一步。<br />
　　而大陆的公众第一次得知他，是在河北晏阳初乡村建设学院。这位来自台湾的建筑师用茅草、树枝、旧木头，和一群没有建筑经验的志愿者盖起了生态厕所与几栋生态楼房。<br />
　　在此之前很长一段时间里，谢英俊一直是一个常规意义上的建筑师。1977年台湾淡江大学建筑系毕业后，他设计了许多大型现代化厂房。上世纪八十年代起，谢英俊多次游历大陆，从事民居及戏剧考察；在台湾承接了许多政府公共工程的设计，独到的人文理念为他赢得众多奖项和业内荣誉。其中新竹县立文化中心，一改观众必须正襟危坐欣赏演出的套路，将开放的环形观众席设计成错落的长凳效果，看演出的时候可以喝茶嗑瓜子，高兴了可以站起来叫好，也可以跷脚盘腿，或者与身边的人边看边聊，就像是回到了传统的戏院。<br />
　　谢英俊将自己的建筑事务所命名为“第三建筑工作室”，意即“非现代非传统”。他使用低污染、可回收的天然建材如石头、竹子和木料，尽量使用台湾本地材料。他还将挖地基产生的土石直接用于建造，让建房过程本身也成为一个能够循环的生态系统，是更为本真的生态建筑。但尽管有创新，谢英俊总体上还是个规规矩矩的建筑师。<br />
　　如果没有1999年的一场灾难，谢英俊的生命不会像现在这样精彩。<br />
<br />
<br />
 <br />
<br />
为世界上人数最少的民族建房<br />
<br />
 <br />
<br />
　　1999年，台湾的“9・21”大地震夺去了两千多人的生命，摧毁了日月潭畔许多山地居民的家园。台湾山地居住着十二个少数民族，其中邵族震后仅存二百八十余人，成为世界上人数最少的族群。<br />
　　邵族从日据时期即饱受流离之苦，1934年日月潭水库电站建设，他们被迫迁移到现住地，族人无地可耕，无屋可居，许多人住在“铁皮寮”或者废弃的货柜车里，人口锐减，连传统的祭仪场地也被破坏殆尽。震灾之后，仅存的邵族人在临时搭建的帐篷里等待救助，与政府僵持几十年的土地抗争，也极有可能借重建之名被一夕铲除。几位一直关注邵族文化的人类学、社会学学者忧心忡忡，担心他们为寻求生计四分五裂，面临灭族亡种的地步。<br />
　　要想挽救濒临消失的邵族族群文化，最好的办法就是保存邵族人聚居的社区，协助邵族人重建家园。因为邵族位于水源保护区、稀有动植物保护区，必须是生态意义上的重建。震后不久的10月中旬，谢英俊被请到了重建现场。<br />
　　虽然是第一次接触到邵族，此前也没有灾后重建的经历，但在人心惶惶的残垣断壁现场，谢英俊已有一个完整的构想，对工程造价也了然于胸。<br />
　　灾后重建的模式一般是政府提供一定补助，不足部分由银行向灾民提供贷款，一定时间内政府负担利息。且不论银行会不会把钱贷给基本没有偿付能力的贫困山民，这种过度依赖垄断性的建筑工业体系和现代金融体系的模式，让很多人在重建过程中因过度消费而破产。专业建筑商在重建资金中占据一部分利润，而那些原住民受灾户只能坐在路边看着别人替自己盖房子，靠失业补贴酗酒沉沦。<br />
　　谢英俊有信心仅用四分之一的耗费恢复邵族社区，帮助重建邵族的文化传统和民族自信。<br />
　　10月29日，谢英俊和他的设计团队共五人，带着帐篷、睡袋、个人用品和营造工具来到了这里。没有大机械，没有大量的施工人员，就要靠这几个人力量，开始一个部落的恢复和重建。不，也许不应该这样说，跟他们站在一起的，还有所有的邵族人。<br />
　　邵族的情况有点像大陆农村，年轻力壮的都进城打工，留下无法进入城市的老弱妇孺。谢英俊来之前已经周密设计，最初曾经设想过一种模式，自己和朋友们在外界筹集善款购置必需的重建材料，在邵族内部用以工易工、共食、完全摒弃金钱交易的方式进行重建，但最终没能行得通。而且，居无定所的族人，对这个建筑师所说的用轻钢和竹片建房子的提议也不感兴趣，尽管谢英俊一再强调他设计的房子抗震性好，又通风透气，但他们还是向往那种在电视里看到的现代化建筑。再说了，他的重建方案并不招标委托专门的建筑公司，这样盖出的房子能住吗？<br />
　　没有办法，谢英俊只能雇请了几个邵族人先盖一幢示范房，一边干一边摸索，改良施工材料和方法。他们在这里盖房子，看的人远比干的人要多得多：这些人也能盖得了房子？怕是等不到地震自己就会倒掉了吧？<br />
　　让族人意外的是，仅用了不到半个月，第一幢房子落成了。<br />
　　三天之后，族人决定就按谢英俊说的办。族人开了整整十天会，谢英俊在研究了邵族文化传统之后提出，不只为这些无家可归的灾民建起一个栖身之所，设计中还有图书馆、村落教室、工坊以及举行邵族传统祭典仪式的祭场，根据邵族祖灵信仰的传统，每一户都有专门摆放祖灵篮的神龛。族人分别就房屋设计、施工方法、编组方式、薪金问题、分配问题等等进行了讨论。11月30日，他们按四十一个祖灵篮分四十一户抽签。此时，谢英俊和朋友们的奔走也有了结果，各界捐助款纷纷到位。<br />
　　12月5日是正式动工日子，按照邵族传统举行了祈福仪式，为了这中断多年的仪式，许多人翻出了久已不穿的民族服装。<br />
　　谢英俊也由一个传统意义上的建筑师，进入了社会运动领域。<br />
<br />
 <br />
<br />
<br />
以建筑参与社会运动<br />
<br />
 <br />
<br />
　　为了全力投入原住民部落重建，谢英俊将第三建筑工作室从新竹迁到了日月潭。他将邵族重建方式称为“协力造屋”：将建筑去工具化，去专业化（设计除外），让建筑回到使用者手里。留在当地的邵族人，不论是上了年纪的大妈、大伯，因酗酒损伤骨头换过髂关节的人，还是失学后在城市游荡过又回到家乡的年轻人，一一收罗在内。原住民本已在社会边缘，而谢英俊招至麾下的这些人，大多在部落中也没什么地位，让这些不只被主流社会排斥、即使在部落内亦受到冷落的边缘人加入重建工作营，藉由集体劳作凝聚强化部落族群意识。<br />
　　邵族重建的另一大特点是大量义工参与。12月11日，第一批五十名义工来到施工现场，他们分别是学生、教师、社区工作者及各界人士（后来成为“宝岛义工团”）。他们自费、自备工具及伙食，固定于每个星期六凌晨四时自台北集合出发到邵族社区，成为重建的主力部队。在谢英俊此后的部落重建工作中，累计义工超过一万人次。<br />
　　12月31日，世纪之交，第一批震灾全倒户重建房屋落成了八户，举行了邵族传统的入厝仪式。每户入厝户提供三道传统菜招待谢英俊和他的建筑团队及族人，还有邵族传统舞蹈表演和营火晚会，狂欢持续到凌晨三点。<br />
　　2000年1月14日，按照谢英俊与族人事先约定，在新落成的部落教室，邵族母语研习课上了第一节课。<br />
　　1月21日，在建筑工地上召开了一次会议，制定了未来的社区规范，包括不得更动或者破坏房屋外观、住户都必须去部落教室上课以及未来供水用电问题等等。2月16日，邵族部落重建土建工程完工，召开社区抽签大会，并成立了社区临时管理委员会。<br />
　　邵族部落的住房重建仅用三个月就完工了，但谢英俊和他的团队并没有就此离开这里。要做的事情还有很多，保留恢复邵族传统文化的重建工作才刚刚开始。在随后的三月份里，他们在新建的社区里恢复了中断近二十年的播种祭。虽然连寻找谷种都大费周折，但仪式总算如期举行，大家还决定要按期举行未来各项祭仪。<br />
　　新社区建得很漂亮，那些边缘人在部落中的地位也无形中得到了提高，并成为协力造屋工作队骨干力量。工作队维持固定成员与流动人员五十五名，大多是山地原住民。3月5日，台中县泰雅族松鹤部落向邵族求助，工作队又参与了松鹤部落的重建工作。<br />
