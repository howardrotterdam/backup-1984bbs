进大陆第一步：盖厕所<br />
<br />
 <br />
<br />
　　2003年春天，谢英俊在香港大学演讲。有位朋友问他，是否愿意把协力造屋理念推广到大陆，让八亿农民受惠，他欣然接受。2004年3月，经这位朋友介绍，谢英俊在北京见到了大陆“三农”专家温铁军，“温老师非常清楚我在做什么，只用五分钟把事情搞定。”<br />
　　谢英俊随即登上南去的火车，来到了位于河北定州农村的晏阳初乡村建设学院。<br />
　　学院所在的翟城村不富也不穷，算是华北平原上的中等水平，村子新起的房子红砖到顶外贴磁砖，家境好些的，还像城里一样用上了宽大的铝合金窗。也许是因为“9・21”大地震的记忆太过惨痛，这样的房子出现在同样是地震高发区的河北，谢英俊一看就皱眉头：一点抗震性都没有。<br />
　　当时，乡建学院正准备盖一个厕所，常见的公共厕所需要近两万元。当时卫生部正在农村推广粪尿分离式厕所，但谢英俊一看图纸便知是纸上谈兵。在台湾，谢英俊以“动手做建筑师”闻名，每年都带学生一起亲自动手建房子，将理论与实践结合，恰恰是他的强项。<br />
　　2004年夏天，谢英俊设计的厕所建成了，造价两千元，大部分是人工开支，材料几乎没花什么钱：墙是村民不要的柳树苗扎成的，外面抹了一层掺了麦秸的粘土；屋顶是旧木头、废树苗搭起来的，上面再覆了一层草顶棚；门是用苇席钉的；男便池是用废弃的涂料桶做成的……台湾来的建筑师让所有人特别是村民都大吃一惊：这不就是老辈人用的茅坑嘛。因为建这个厕所的时候都是些没干过泥水活的学生娃娃志愿者，他们说了，抹墙抹得平是技术，抹不平是艺术，学院的这个厕所，每一面墙都抹得非常“艺术”。<br />
　　谢英俊却信心满满，将厕所建在了距宿舍不足十米的地方，他说这样的厕所保证不会有味道，而且，不会有蛆虫。厕所的照片被学院贴到了网上，名之曰“生态厕所”。<br />
　　原因有四：<br />
　　一、节水。华北平原严重缺水，谢英俊设计的厕所不用水冲，节水是一大特点。<br />
　　二、无害。无害化处理的前提是粪尿分离。<br />
　　三、无味无虫。<br />
　　四、不破坏环境。砖和水泥的制造过程中都要消耗能源，释放大量二氧化碳，按照《京都议定书》，将来，向大气中超量排放二氧化碳是要付费的，而树木秸杆在生产过程中能够固定二氧化碳，按照规定还可以收到补偿呢。<br />
　　其实，这个厕所真正的全称应当不止是“生态厕所”四字，至少应当加上“可持续”。首先所有的建材都来自土地并且可以回归土地。墙都是土墙，土分土骨和土肉，上层富含有机质的部分叫土肉，下层部分叫土骨，建筑只取下层部分，不会影响土地使用。不论是麦秸、旧木、树苗还是草扎的顶棚，用后都可以还田。<br />
　　同时在经济方面也可以称之为“可持续”，就地取材，可以最大限度地降低甚至取消运输成本，使用自产原料可以降低对货币的依赖，农民只要有劳动力，有时间，就可以盖，自然是可持续的。<br />
　　盖这个厕所只是谢英俊牛刀小试，醉翁之意不在酒。<br />
　　中国农民忙碌一生，为的就是一件事：为儿子盖房娶媳妇。但是，现代化的营建制度是一个完整的体系，每一个环节都要将农民的血汗钱吃掉一大块，一个农民攒十万块钱可能需要一辈子，花掉却只要一瞬间，这样花钱太冤了。<br />
　　按照谢英俊的设计，除开建材，盖房所需只剩了劳动力的支出。中国农村什么都缺，就是不缺劳动力，如果一个村子里几年内准备建房的有二十户，那么这二十户人就可以组成一个建房合作社，利用农闲时间互助建房。而且互助也不限于换工合作，还可以延伸到资金互助以及其他，这样，只要你有劳动力、有劳动意愿，任何人都可以建得起房子。最重要的是，通过盖房可以让合作机制延伸到农村的基层建设，比如修沟渠、修路、修水库，可以跟社会经济各种条件结合在一起……<br />
　　这一切，才是让谢英俊和温铁军一拍即合的真正原因。<br />
　　谢英俊要在八亿农民中，以建房为契机推动一场社会运动，却是以乡建学院的一处厕所作为发端的。<br />
　　厕所建成后，谢英俊结合华北平原的实际情况，为当地农民设计了两款生态建筑。他四处在村里找愿意按照他的设计方案建房的农户，但农民一听他要盖的是火炕土墙的泥巴房子，吓得连连摇头：这木梁木檩、麦秸泥墙、灶台火炕，是八百年前的老皇历了，盖了这样的生态房子，我儿子这辈子就别想娶上媳妇了！<br />
　　连试了几户人家都碰了壁，学院里的人有些着急，谢英俊却见怪不怪：“没问题的，我们先在学院盖一栋示范房好了。”对他来说，这已是再正常不过的事情，当初在邵族，一开始遇到的也是完全一样的情况。<br />
　　谢英俊在乡建学院成立了专门的乡村建筑工作室，建了三所示范房，“地球屋一号”用木头做构架，“地球屋二号”用轻钢，一号、二号都是专为北方农村设计的三开间二层小楼，面积一百六十平方左右。与农村通行建房方式最大的不同在于，这房子是框架式的，一号的框架为原木结构，先由梁柱斜撑等构件相互咬合成框架，再填充草和土构成的墙体。由于房屋的主要载重由框架承担，墙体只负担自身的重量，墙倒屋不塌，同时墙体对整体起到辅助的增强，所以抗震性能远比砖混结构优越。二号的框架用的不是木料而是轻钢型材。谢英俊是这么考虑的：建“一号”需要接近二十方木料，材料成本二万元左右，大部分是木料支出，如果农民家里有木料的话就可以省下这笔钱，如果没有木料，使用同样符合环保要求的轻钢，还可以少花五千元。“亚齐”则是一座是颇具热带风情的吊脚楼，是专为印尼重建示范房。<br />
　　建设之中、建完之后村民们对生态房刮目相看了。<br />
