													<h2>毛新宇是新时代的领路人</h2>
						
						
																											<div id="postmessage_263861" class="t_msgfont">毛新宇是新时代的领路人<br />
作者：木然 2009-9-29 14:31:23 发表于：博客中国<br />
<br />
<br />
<br />
<br />
毛新宇，男，1970年1月17日生，汉族。1992年7月毕业于中国人民大学历史系，1992年9月至1995年7月在中央党校理论部攻读硕士学位，2000年进入军事科学院攻读博士学位，同时还参军入伍。2003年7月获得博士学位，现任军事科学院研究员。1993年加入中国共产党，现任中国人民解放军军事科学院战争理论和战略研究部副部长、中华全国青年联合会常委、全国政协委员。2008年7月，被任命为军事科学院战略部副部长，走上高级领导岗位，为副军级，大校军衔。说前面这些平淡无奇，再加一句话就气吞山河、举世瞩目、跌宕起伏，此人是毛泽东的亲孙子，他似乎比毛泽东有创意，正所谓长江后浪推前浪，后生可畏。学习他的理论观点，一定会把我们领进新时代。那么他会把我们领进一个什么样的新时代呢？<br />
<br />
一、毛新宇把我们领进一个休闲时代。他指出： “希望全国人民能永远记住新中国的缔造者和伟大领袖，比如我的爷爷毛泽东。我觉得应该采取多种形式、广泛地宣传领袖的事迹和思想，比如将毛泽东的诞辰和忌辰定为法定节假日或纪念日。”这个想法好，经过毛泽东批判马寅初人口论之后多生了三亿人，这多生的三亿人口相当于美国总人口。按毛泽东想法，人多力量大，众志成城。如果发生第三次世界大战，死三分之一无所谓，再批一个马寅初就会又生出来三亿人口甚至更多。令毛泽东遗憾的是，第三次世界大战没打起来，多了三亿人也就那么多着。人一多，事就多。事一多，领导就很忙。要想让大家事少，领导不忙，就得休息，就得放假。把毛泽东生与死的日期当作假期休息日成就了民众和领导的好事。纪念毛泽东就放假，国家就稳定了，领导事就少了，社会就和谐了，毛泽东就永远活在人民心中了。大家可以旅旅游，到一趟大城市，美美容。这不但带动了旅游事业的发展，而且还解决了很多人的就业问题。说不定通过毛泽东纪念日还能解决金融危机呢，因为毛泽东实在是神力无边。<br />
<br />
毛泽东说他自己是马克思加秦始皇，为了以示尊重，爱屋及乌，把马克思、秦始皇生死日期加上，共六天假日，全国人民与毛泽东同生死两界，天上地上共欢乐，好得很。如果再把中国的孔子、孟子生与死的日期加上就更好了，把世界上所有名人的生死日期加上最好。果真如此的话，估计一年三百六十五天都放假也不够。这才是太平天国，大同世界。想象一下，天天放假，天天花着印有毛泽东头像的百元人民币，要多好有多好，好上加好，要多幸福有多幸福，不幸福才怪。<br />
<br />
二、毛新宇把我们领进一个伟人崇拜时代。他指出：“中国1000年里不可能再有毛泽东这么伟大的人”你看看人家，说这话都不用证据，多有才华！林彪曾说：毛泽东是五千年才出一个，如果再加上毛泽东孙子的一千年就是六千年。六千年才出一个毛泽东，真是不崇拜不得了呀。<br />
<br />
他说：“我从小就知道我的爷爷是毛泽东，也觉得我爷爷很伟大。3岁我就开始唱《北京的金山上》，真的就跟歌里唱的一样，觉得爷爷是金山上那颗光芒四射的红太阳”。因此，毛新宇从5岁就开始背诵毛泽东诗词，他儿子毛东东两三岁的时候，邵华就教他唱《东方红》。毛新宇说：东东“比我要聪明，那个时候就已经能背3首我爷爷的诗词了。”要别人崇拜，先从自己做起，榜样的力量是无穷的。<br />
三、毛新宇把我们领进一个新血统论时代。他指出：“说句真话，我这辈子有3个没有想到。我本以为大学毕业，我会继续研究我很感兴趣的明清历史，没想到我母亲坚决要求我去研究毛泽东思想。现在看来，这个决策完全正确；第二，入党我也没有思想准备。我入党的日期特别有纪念意义，是1993年12月16日，10天后就是我爷爷诞辰100周年；第三，我没有想到我会参军入伍，更没有想到自己会当将军。”与此相应的三个问题是：第一，兴趣是学习的第一老师，不按自己的兴趣去研究明清史而去研究毛泽东思想有悖于个人自由，我对毛泽东的孙子表示同情，对他这种献身于毛泽东思想研究表示钦佩。第二，入党程序很复杂，得主动申请，得写思想汇报，得接受党组织的观察与培养，他自己没思想准备就入党，看来也有悖于自由，他是“被入党”的。第三，毛泽东的孙子不是大校吗？何时变成将军的呢？那就只有一个答案：现在不是将军，将来一定是将军，毛泽东能未卜先知，毛泽东的孙子也能。再说不就是一个将军嘛，在他那个位置，虽说不能所有的事想啥是啥，想谁是谁，可想个将军还是能办到的，谁让人家是毛泽东的孙子来着！<br />
<br />
孙子哪能不沾爷爷的光呢？否则血统论就没了市场。这要是在“文化大革命”反血统论会面临灭顶之灾，遇罗克就是证明。改革开放之后血统论在太子党那里涛声依旧、市场巨大、潜力无限，毛新宇也没能免俗。他在被问到自己获得的荣誉和地位时，回答得就很血统：“我想这是全国人民对领袖的尊敬和爱戴，也是党中央对开国元勋后代的培养和关心，也可以直接说是沾了我爷爷的光。”<br />
<br />
四、毛新宇把我们领进一个毛泽东思想指引航向的时代。他把毛泽东思想分为三部分：“第一个就是毛泽东哲学思想，这是毛泽东思想最精华的部分；第二个就是毛泽东军事战略思想；再一个就是毛泽东政治韬略”。有毛泽东思想就不用别人思想，“我爷爷他老人家的智慧，是那些教授都比不了的”。教授都没法比，全国人民更没法比，毛泽东一个人的思想就足够中国人享用。取消大学极为正确，国家改革开放之后办大学没必要。马克思主义、邓小平理论、“三个代表”、科学发展观都不用学，毛泽东思想仍然是中国的指导思想，正如毛新宇所说：他“ 爷爷的著作博大精深，永远不会过时”。<br />
<br />
他爷爷的经济思想更具独特魅力：“中国现在面临的巨大问题还是完成工业化，不要忘了，中国真正的工业化建设是从毛泽东开始，中国要想完成工业化的历史任务，就一定要考虑毛泽东思想”。他充满自信地说：“你说我武断也好、别的什么也好，我觉得如果没有毛泽东思想指导、没有马克思思想指导，我们工业化和现代化的任务是完成不了的”。马克思思想都排在毛泽东思想的后面里，看来我们得倒时差，进入时空隧道让时光倒流先看到毛泽东才能看到马克思，没有毛泽东的引见，马克思说不定还见不到。<br />
<br />
五、毛新宇把我们领进中国文化独尊时代。他指出：“毛泽东思想把马列主义中国化，把它变成了我们党的思想，这项事业是永恒的。”据研究毛泽东的专家学者说：毛泽东主要是看中国古代的书，马克思主义的书他读得并不多。马克思的《德意志意识形态》、《资本论》及马克思晚年思想变化的书信都没有看过。在毛泽东那里马克思主义中国化的实质是中国文化独尊化、大一统化、专制化和权术化。<br />
<br />
据网上介绍：邵华在读书学习上受过毛泽东直接指导和影响，她姐姐刘思齐和毛岸英结婚后，她常跑进中南海，被戏称为“小尾巴虫”。毛泽东对这位革命遗孤关切甚厚，点拨她读史书，“死记硬背，算是知道了点皮毛，学历史要多读史料，多思考，能把‘为什么’都说清楚，这一课才算学好了。”毛泽东还与邵华一起谈论古诗词。谈陆游，挥笔为她写下了《夜游宫》；谈王勃，写下了“落霞与孤鹜齐飞，秋水共长天一色”。为了让毛新宇接上他爷爷的精神血脉，邵华请王震给中央办公厅写了一个批示。“让我去爷爷的书房看毛主席批的《二十四史》和哲学笔记，那时候我上大学。”毛新宇还自豪地说：“爷爷批注的《二十四史》我是第一读者”。他却没有说毛泽东批注过马克思的哪本书，真是咄咄怪事！ <br />
六、毛新宇把我们领进一个对计划生育进行检讨的时代。众所周知，计划生育是我国的基本国策，毛新宇没把这一国策放在眼里，他说他有两个孩子，很显然违背了计划生育。这让我想起了毛泽东，他让司局干部搞法治，他自己搞人治。毛泽东的经典论说是：要什么法治，《人民日报》一篇社论什么问题都解决了。毛泽东的孙子深得真传，要什么国策，生就是了，一生就什么问题都解决了。毛新宇与妻子刘滨育有一子一女。儿子毛东东出生于2003年12月26日，与毛泽东同一天生日，女儿毛甜懿，出生于2008年8月28日。你看人家，正是置国策于不顾，才能生得及时，生得伟大，活得光荣，只要庆祝毛泽东的生日，重孙子也得不庆祝，不庆祝还真不行。你还别说，毛新宇真乃一神枪手也。<br />
<br />
七、毛新宇把我们领进一个评职称坐飞机的时代。我们没有从他的简历看到什么时候评上助理研究员、副研究员的，只看到了他是正研究员。一般地说，大学职称评定是这样的：助教、讲师、副教授、教授，一般人评到教授时大致十到十五年甚至更长的时间。研究员的评定也大致如此，可人家一出山就是一个研究员，怪不得他和爷爷一样都看不起大学教授。在他爷爷那里是对教授不屑，在他那里是坐飞机，教授们只好在地上走，坐飞机的哪能看上地上走的。如同国民党的军官和士兵，军官骑马飞，士兵跟着跑。伟人就是伟人，伟人的后代还是伟人，不服不行，不崇拜不行，谁让爹妈没给那些教授那么大的智慧呢。话得说回来，毛新宇的研究员是由有高贵的血统带来的，教授们没有高贵的血统，也就没有那么高的智慧。<br />
<br />
读毛泽东的话得细细品味才能有收获，看毛新宇的观点也得有耐心。他的观点随空飘荡，经常不知所以，前后逻辑又不一致。这很符合游击战术，东一枪西一炮，不按套路出牌，蒋介石打不过毛泽东，我们的观点打不败毛新宇都是因为没有领会游击精髓的智慧。<br />
<br />
云里雾里看不清，跟着走就行了，走了就进入新时代了。这个时代，老年人领教过，但已成过眼烟云。青年人没见过，它通向未来。毛新宇是70后，跟着他就会走进新时代。不过，在走向新时代之前，一定要问问老人。俗话说得好，不听老人言，吃亏在眼前。</div>

							
							
							
							
