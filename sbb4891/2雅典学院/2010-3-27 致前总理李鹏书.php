――致前总理李鹏书 <br />
尊敬的李&nbsp;&nbsp;鹏同志：<br />
<br />
今年是国庆60周年，本该是普天同庆的日子，但京城却是愁云惨雾，风声鹤唳，党政机关惶惶不安，军队警察枕戈待旦，又是集训县委书记，又是集训公安局长，又是要军队做好非军事斗争准备，好像正在准备另一场“陆肆”。<br />
<br />
今日党和政府与全国人民为敌的形势，盖因1989年镇压以反官倒、反腐败为主题的学生运动所致。对“陆肆”的错误处置主要责任者是邓公 和您。听说您急于发表“陆肆”日记为自己辩解，这大可不必，在历史的耻辱柱上您已经被刻在了第2号。因为向老人们送情况的是您，在政治局常委统一思想拿主 意的也是您。据当时北京市委书记李锡铭向中央政治局扩大会议报告：经北京市与戒//严部队指挥部、公安部、中国红十字会、北京各高校、北京各大医院等方面 再三核实，有241人死亡（学生和群众218人，戒//严部队23人）。 1“陆肆”波及全国63个城市，镇压后的政治冲击波殃及千千万万的学生、干部和市民。这个巨大的全民性的创伤至今不能平复，亦因您出于个人名节私虑，置人民伤痛、党国大局于不顾，坚决反对给“陆肆”平反。<br />
<br />
经过二十年历史之检验，那次镇压的长远后果是为权贵资本主义，即官僚资产阶级的形成保了驾，护了航，在历史上没有任何进步意义。“陆 肆”处置之大错特错，已如十日并照之明。全党上下，如果公决，95%以上的同志会同意平反，我遇到的党内同志都是这个意见。您不能与全党保持一致，错用元 老重臣的身份，将党和政府推向与人民为敌的地步，午夜扪心，能无愧疚？<br />
<br />
近读段祺瑞轶事深深打动了我。1926年3月18日，共产党人李大钊，国民党左派徐谦率领几千学生到执政府请愿，卫队开枪，死47人， 伤150余人，是为三一八惨案。事发时段祺瑞未在铁狮子胡同执政府，而在吉兆胡同私宅与友人对弈，绝非预谋。他完全可以逃避责任，或惩戒下属保全面子。但 他作为政府首脑，主动承担了责任：查清死难同胞的名字，厚加慰恤；亲自出席悼念三一八死难同胞大会，当众长跪不起，以示赎罪。执政府总辞职退出政坛后，他 一直吃素，不听家人和医生的开斋劝说，直到终老。 2 如 果段公强调有苏俄帝国背景，有革命党操纵（他们有证据）群众，图谋推翻政府，镇压动乱有理，也能胡搅蛮缠几年。那时时局动荡，战乱频仍，他躲进天津公寓， 没人能奈何他。或曰段公做秀，厚恤死者、当众下跪，可能有请求国人谅解的成份，下野后在天津做寓公吃素就解释不通了，这不是做秀，是终身的愧疚，终身的忏 悔。读书至此，我忽然觉得段祺瑞的人格比您高大起来。<br />
<br />
进思建功，退思补过。公宦海余生，正当补过之时。解铃还须系铃人。建议您学习段祺瑞。由你主动提出为“陆肆”平反，帮助党卸下这个历史 包袱，实现执政党与全民的和解，与知识分子的和解，与学生的和解，解除党和政府与民众的这种长期对立的状态。您应该有“我不下地狱谁下地狱”的精神，以无 愧于您的养父周恩来。周恩来在遵义会议主动承担五次反围剿失败和长征初期指挥失误的责任，使党完成了路线转变，为开辟中国革命的新局面奠定了基础。他在党 和红军中的威信不是降低了，而是提高了。您做这件事情肯定比发表陆肆日记辩解诿过漂亮。您帮助党过这个坎，帮助中华民族掀过这一页沉重无比的仇恨对立的历 史，您的形象将因此而改变，党的罪过也将得到人民的宽宥。望慎思之。<br />
