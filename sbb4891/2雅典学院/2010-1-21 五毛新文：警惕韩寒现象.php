																											<div id="postmessage_444621" class="t_msgfont">近来在我国舆论界突冒出个韩寒现象？在网上被炒得火热。<br />
本不晓得其为何鸟？ 为何而红？出于好奇就开始关注之。<br />
原来此人是个上海八零后，一查其即无学历，又无特长，一幅掉人堆里就找不着了的主？搞不懂其因何而红？<br />
具说是因出了几本书而红？忙去书店搜寻，一问竟没有？又去图书大厦寻找也无？不是说是畅销书吗？<br />
后终于搜到一本其著作《三重门》，阅后感到平淡无奇就是垃圾啊？真后悔为此占用我半个小时的时间。<br />
更令人困惑的事，在网上则看到大量宣传的他帖子，不止如此南方系媒体和美国时代周刊，百度等都把其做为中国年轻世代的“领袖”而推出专访专辑为其造势宣传？连其自已都声称在不久前奥巴马访华时，美国大使馆都当他为中国的青年领袖而邀其参加奥氏和青年的对话会？真是不可思议？美国佬何以如此看重这么个普通青年？<br />
通读了一遍其所有的博文，发现几大怪异？<br />
其一怪：其发之文不象是一个20几岁的年轻人所写。<br />
其文可以用“苦大仇深”形容概括，通篇都在控诉社会的黑暗，在发泄对现实的不满？彷佛中国就是人间地狱？其文核心要精就是仇视社会，贬损中国现实，并亲美媚日捧台湾。<br />
中国当然有不足有黑暗的角落远不是个十全十美的理想社会。但正如评价一个人，她既有优点也有缺点，而一个正常的人应以一客观的心态看之，既要看到她的不足甚至黑暗的地方也应看到她的优点而不是全盘否定！因为中国纵有不足但谁也不能否认总体是向好向上这一事实！而在其笔下处处透出对中国的不堪？甚至仇恨？如祖国不是我母亲，我妈是我妈，她叫周XX。及生为中国人的悲哀，来生再不愿做中国人等等。甚至到澳洲一游也要借机贬中捧洋？这些都不象个一只有20几岁的世界观还没完全成型的年轻人的认识啊？<br />
不禁奇怪？中国怎么他了？他生于八十年代，既没挨过饿也没被迫害过，且正处中国经济起飞国家开始崛起之时，赶上了好时代享尽发展红利国家怎对不起他了？<br />
难道是因祖上是国民党或地主被镇压过？和G有历史仇恨？不然为何如此仇视新中国？<br />
其第二大怪：其发表之文量极大且文笔和思维怪异。<br />
众所周知，一个人的精力和知识是有限的。<br />
再有才华的人也不可能涉猎极广无所不包。他也就只能涉及自已所喜好和力所能及的领域。<br />
而其博文则无所不包，不止社会政经连军事等无所不有？且量超大？另外一个人的思维和文笔是一种长期性习惯是你所独有的不可能说变就变，而在其博文中除贬损社会攻击政府这一主题是一致外别的完全不同。其文笔思维混乱，在个别文中竟有在通篇的简体字中偶有夹杂着繁体字的怪现象？且其潜词用语多有台湾的用语，如网上台湾人常用来攻击大陆的“五毛党”，“大陆屁民”，“天朝”等用语？还有指中国馆是用“屁民”的血所染红并借拆迁纠纷等局部问题指政府和黑帮是一伙进而把民众和政府对立起来挑拨双方关系，其腔调就象我们所熟悉的网上台湾人或民轮在谩骂攻击？<br />
由以上现象看，难道其发之文很多是出自台湾人之手，且不是一人所为而是由多人代笔？只是集中冠以韩之名发表而已？看来在其背后可能有一个专门的写作班子，为他炮制文章！不然就不可能有这么多文笔思维不同且量极大的攻击性文帖出来。<br />
其第三怪：原本无名之辈突然象从地底冒出一样被集中宣传造势炒作？<br />
很明显是有股势力在为其造势炒作。<br />
就在半年前，大众还不知其为何鸟？而今不但在网上连平面媒体如南方报业，新民周刊和新世纪周刊等杂志报纸纷纷对他大加赞美吹捧？南方周末推出他专访主版，将其捧为“八零后意见领袖”“青年偶像”，“有良知的知识分子”（反G反祖国就是有良知），甚至已喊出应让其当市长，及教育部长等鼓躁和吹捧。彷佛他已成为一颗新明星一般？而此人却是个不懂感恩仇视自已祖国的人渣却被他们捧为中国的未来之星？青年领袖？这难道不是另有用心？<br />
