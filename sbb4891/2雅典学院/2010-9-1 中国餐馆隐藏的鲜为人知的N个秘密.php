													<h2>中国餐馆隐藏的鲜为人知的N个秘密</h2>
						
						
																											<div id="postmessage_807192" class="t_msgfont">不得不说，这是一个忙碌的社会，以至于很多人都不愿意下厨房，而在餐馆里解决。可是，食物光鲜的外表下，你知道这里面隐藏着哪些不能说的秘密吗？<br />
<br />
　　可能你知道，可能你不知道，但是我想说的是，你，肯定没有知道的多。因为我在这个行业干了12年了。从今天起，我就一一给你们揭露一些这里面的猫腻。。。看看没坏处，多聊点儿点儿东西总是好的，觉得我写的好，各位顶贴啊！！！<br />
<br />
　　秘密1：最暴利的菜是土豆丝这样的普通家常菜<br />
<br />
　　很多人认为餐馆里海鲜最暴利，这就完全错了。往往越便宜的菜，比如炒土豆丝之类的，利润空间最大。虽然现在由于大蒜、生姜的价格出现了翻倍的增长，也出现了“蒜你狠”和“姜一军”这样的新词，不明白意思的可以去互动百科查一下，但这依然挡不住他们的暴利。其实中等价位的菜，是性价比最高的。<br />
<br />
　　下面我给大家算一下成本：一盘儿土豆丝盖饭，一般的饭店定价在6块钱到12块钱左右（地段、档次不一样，定价肯定不要用），但他们的成本基本都是一样的：按照今年7月份土豆的成本来算：1斤应该在8毛钱到1块5之间，超市的可能贵一点，可是餐馆基本和菜贩子是常客，拿到的价格比一般人要低。1斤土豆一般能做至少4、5份盖饭，平均下来也就2毛到4毛钱，即便加上米饭，油盐等调料，甚至把房租水电、工人工资都平均下来，也不过1块钱成本。所以土豆丝的纯利润应该在500%到1100%左右。想想吧，你被宰了多少？<br />
<br />
　　秘密2： 厨师有时候会发泄，会往菜里吐痰<br />
<br />
　　其实所有的厨师都知道这件事情，厨师也是人，都有情绪的好坏，在遇到顾客退菜的时候；跟老板同事闹别扭的时候；在遇到烦心事的时候，怎么宣泄？一些不道德的厨师便将自己的唾液和痰吐到菜里面，拿客人发泄，反正经过翻炒之后，完全看不出来，你就是吃也不会吃出来的。<br />
<br />
　　在一个饭店的厨师班子里 ，总会有那么几个人有这种不良的嗜好，但大家都是同事，抬头不见低头见的，所以很少有厨师会把这个事情说出来。<br />
<br />
　　秘密3：活鱼活虾都是摆在外面给你看的，端给你的都是从冰柜里拿出来的死鱼死虾，经过加工之后，很多人已经尝不出来了<br />
<br />
　　很多人认为，请客吃饭不上点儿鱼啊虾啊不够上档次。于是总是喜欢点上一两条，点餐后，总有服务员拿网兜捞出一条活蹦乱跳的鱼，让你过目。可是，真正上桌的，还是你看到的那条鱼吗?<br />
<br />
　　其实在很多餐馆，都有猫腻，这几天九头鸟餐馆不是被曝光了吗？给客人点完鱼后拿到后厨换掉，这种猫腻在北戴河、青岛等很多海滨城市更为猖獗。因为他们宰的就是游客。<br />
<br />
　　在这里，告诉你从互动百科看到的一个小窍门：一般来说，鱼眼突出、鱼肉有弹性、表皮完整的大多是鲜鱼。有些鲜鱼在蒸制过程中肉会裂开，这也是新鲜的标志。鲜虾的尾巴是张开的，头是黑色的，一眼就可以辨认。<br />
<br />
　　秘密4：一般情况下，周一的菜最不新鲜了，最好别在快打烊的时候进餐<br />
<br />
　　餐馆厨房一般供餐的原则是─ ─先进的货先卖掉，这代表最旧的材料最有可能上到你的餐桌，这样他们才有可能在东西坏掉前消耗光。<br />
<br />
　　因为有些供货商也要在周末休假下，有时候星期六早晨他们就完成最后一批进货后就不送了，这意味着星期一上馆子时，食材已经放了至少两天。<br />
<br />
　　此外，也别在餐馆打烊前去用餐，这时厨师一般都会应付了事，而且厨房已经进入清洗消毒阶段，你的食物接触到清洁剂、洗涤剂的几率会大大增加。<br />
<br />
　　秘密5：材料越复杂的菜，可能越不新鲜 ，最好别点这样的才　<br />
<br />
　　餐馆处理不新鲜的原料有两个办法，一是加重口味，用红烧、酱焖等做法，掩盖不新鲜的口感和气味;二是和其他原料放在一起，甚至把主菜变成配菜。 比如炒肉丝一般要用新鲜的肉，但麻辣香锅里的肉啊、虾啊、就未必了。还有些海鲜煲、素什锦之类的菜，因为原料复杂，里面也常常隐藏着几种不新鲜的原料，你一般难以发觉.　<br />
<br />
　　所以，我建议，多点“简单”的菜。如果原料只有一两种，最好不超过三种，那其中就很难混入不新鲜的东西了。</div>

							
							
							
							
