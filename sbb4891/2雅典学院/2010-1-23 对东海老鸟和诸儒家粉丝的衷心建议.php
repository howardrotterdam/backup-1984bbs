孔子是中国人永远的精神导师<br />
　　<br />
　　文@金满楼<br />
　　<br />
　　杨澜在观看电影《孔子》之后，发了篇博文叫《孔子是个失败者》，笔者阅后不免哑然失笑：如果孔子是个失败者，那杨澜这样的又算什么呢？历史会记下极少数人的名字，我想，自以为很有名的杨澜女士，应该不在此数。孔子的思想与哲学照耀了中国二千多年的历史（并将持续），如果他是失败者，我真不知道用什么词来形容杨澜之流了。杨澜如果没有读过或者读懂《论语》，最好先闭上自己的鸟嘴，别自以为是的在这里瞎误导、乱煽风点火，不然，只能助长一些别有用心的人和愤青们的反传统浪潮，试问她负得起这个责任吗？ <br />
　　<br />
　　现在的中国早已抛弃了自己本土的传统文化，这是目前社会信仰普遍缺失的根本原因。几十年前，我们引进了几个外国圣人，接受了宗教徒式的红色教育，而当那些伟大人物和英雄时代一去而不复返的时候，留下的只能是一群迷惘的子民和徘徊的大地。多年前，我们的前辈曾兴高采烈的打破了一个旧世界，但是，他们建立的新体系在宗教热情退去后也是如此的不堪一击。<br />
　　<br />
　　在精神层面上，中国人是迷惘空虚甚至是孤独无助的，我们的时代，是没有信仰的时代，或者说是一个假信仰的时代，我们表面一套，背后一套；台上说人话，台下干鬼事，以至于人鬼不分，整个社会就像个假面社会，潜规则盛行，说真话的人遭殃，行潜规则的人获利，老百姓在这个假面社会里被迫随波逐流，为好人好事抑郁唏嘘，对坏人坏事熟视无睹，习以为常，甚至被动的助纣为虐，丧失了最基本的道德底线和做人的根本原则。这样的社会这样的人群，如何能让自己的心灵得到宁静呢？<br />
　　<br />
　　有时候笔者想，我们为什么要提和谐社会，是因为我们的社会不和谐了；我们为什么要提社会稳定，是因为我们的社会不稳定了。在一个信仰体系崩溃的社会，我们现在最需要的是，整个民族心灵的救赎及道德的重建。如果没有道德体系的重建，哪怕是经济再发展，也是空洞的、充满危机的发展。可以肯定的说，社会如果不和谐到一定程度了，经济发展也将停滞，如果到了那个时候才去解决这个问题，恐怕为时已晚。<br />
　　<br />
　　挽救中华文明和救赎民族心灵，我们可以依托的，还是我们几千年的文化沉淀。历史发展是有规律的，历史选择了儒家文明绝非偶然。民国军阀张宗昌是个粗人不假，但有一点却颇值得称道，那就是尊孔。他曾经大量出版印刷过儒学的十三经，据印刷界人士评价，这套十三经可能是历史上纸张、印刷和装帧最好的一套版本。张宗昌曾在曲阜孔庙举办过一次规模盛大的祭孔典礼，礼成之后，他说了这样一番话：<br />
　　<br />
　　“我张宗昌小的时候，是个穷人，又是个粗人，长大之后闯关东，闯到今天，倒成了武人。我今天同许多文武官员来到孔老夫子的家乡，恭恭敬敬的祭拜这位老圣人。祭祀的礼节，我本来不懂的，前天来后，先请司礼官讲解了祭孔的礼仪，参观了庙堂礼器，叫我心里更加敬佩。我的祝辞和讲稿，全是王状元作的，已经印出来了，准备每人发给你们一张，希望大家回去好好地念念，好好地想想，将来就好好地做去。咱们大家要一同向孔夫子学习，才配来祭孔老夫子，这些年有人要打倒孔家店，我看打不倒。现在我拨一笔款重修孔庙寝殿，修完这里，再修奎文阁。打的让他们打，修的我们还是修，陈焕章会长（康有为弟子，孔教会会长）在外国还参加重修孔庙，并且亲自运过大梁，何况是我们？”<br />
　　<br />
　　张宗昌的这段话情真意切，不似他平日作为，后来中原大战，蒋介石、阎锡山、石友三的军队在曲阜大战，就毁坏不少孔庙建筑，就这点而言，这些人尚不如张宗昌呢。<br />
　　<br />
　　最令人不可理解的是，一些反儒的朋友一提到孔子、儒学或者中国古代文化就怒火冲天，似乎中国的没落都是因此而引起。很遗憾的说，这种义和团式的愤怒是完全没道理甚至是无理而弱智的，事实的真相往往在于，一些反儒者连最基本的《论语》都没有好好读完整过，这种大肆侮辱诋毁儒家学术的言论是可耻可鄙的。各位义愤填膺的反对儒家的义士们，扪心自问，有没有完整的、安静的读过《论语》，如果没有，请回去买一本仔细读读再来发言。<br />
　　<br />
　　中国的道德体系和诚信社会的构建，必须从传统文化里入手。非常遗憾的是，由于多年的意识形态摧残，大陆已经不是中国传统文明的最好继承者了。不说台湾，就连近邻韩国和日本都可能做得更好。一些人，使用着祖先的汉字，却在大肆诋毁中国的传统文化，当我们心里决定不再受欺骗而没有新的精神支柱的时候，我们感到空虚，我们感到了无助，正因为如此，我们才会对潜规则奉为正物，我们才会对丑恶现象心安理得，也就是这样，我们的道德一再滑坡，我们的社会一再堕落。如此下去，中华民族将陷入万劫不复的境地，哪怕经济发展如繁花似锦，里面却是腐烂如斯。如此结局，国人当思。<br />
　　<br />
　　时代需要千千万万个拯救传统文明、拯救中国人心灵的先知先觉站出来。我们的社会道德建设需要千千万万的人参与，既需要教育者，也需要被教育者。我们要从点点滴滴做起，我们要教育我们的孩子从小就抵制那些丑陋的现象，抵制那些潜规则，还我们一个干净安全诚实信任的社会和国家。<br />
　　<br />
　　中国的道德体系和诚信社会的构建，除了从本土的、传统的文化入手，别无他途。即便是再现代化、网络化的社会，我们及其后代们仍旧需要仁义礼智信，我们需要忠，我们需要孝，我们需要礼仪廉耻。<br />
　　<br />
　　今天，我们该怎样尊敬孔子？&nbsp; &nbsp;<br />
　　<br />
　　我们需要仁，我们要爱人，也要人的关爱；<br />
　　<br />
　　我们需要义，我们要团结友善，我们要正义；<br />
　　<br />
　　我们需要礼，我们做事做人都要有规则，讲文明；<br />
　　<br />
　　我们需要智，我们要教育，我们要创新，我们要发展；<br />
　　<br />
　　我们需要信，我们要为人诚实，整个社会都要有诚信的氛围，国家才会安定，人民才能安心；<br />
　　<br />
　　我们需要忠，我们要忠于脚下的这片土地，忠于这个托身的社会、社区，忠于家庭和爱人；<br />
　　<br />
　　我们需要孝，我们要善待老人，善待儿童和妇女，我们对养育我们的人要有报答的心；<br />
　　<br />
　　我们需要礼仪廉耻，我们要知道耻，知耻后方知勇。<br />
　　<br />
　　孔夫子曾经无情的打倒在地，但中国人的传统及文化却是永远不会被取缔的，因为我们的根就在那里。</div>

							
							
							
							
