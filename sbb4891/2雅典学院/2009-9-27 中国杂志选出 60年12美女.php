文化大革命期间遭受迫害，周恩来在逮捕书上签字同意[1]，关到被军管的北京公安局看守所[2]，1968年10月4日死于狱中，去世时赤身裸体，身上只有一副镣铐，传闻江青曾让刑事犯在强光下剥光孙维世的衣服，进行轮奸，犯人强奸得逞可以减刑[3]，死后被发现头上被钉进了一颗长钉子。周恩来要求验尸，只得到“尸体早已烧掉”的答复。<br />
