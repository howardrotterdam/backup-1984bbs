孙维世（1922年－1968年10月4日），原名孙光英，四川省南溪县人。革命烈士孙炳文之女。著名话剧导演。<br />
<br />
孙维世. 延安1934年，12岁时在北平贝满女中上学。14岁随母亲到上海参加上海业余剧人协会和东方剧社的进步演剧活动。曾在龚秋霞主演的电影《压岁钱》里演出。1937年随兄长孙泱到武汉八路军办事处，1938年16岁加入中国共产党。抗日战争爆发后，参加上海戏剧界救亡演剧队，后至延安学习。1939年被周恩来从延安带到莫斯科留学，攻读戏剧。在苏期间曾被在苏联养病的林彪追求，未接受。1946年回国，与冯风鸣、张醒芳、郭兰英号称“延安四大美女”。参加华北联合大学文工团工作，随军在陕、晋、冀活动。1949年12月，随同毛泽东出访苏联。1950年任中国青年艺术剧院导演，在排练《保尔・柯察金》中与该院演员兼导演、有妇之夫金山相恋，后结婚。<br />
