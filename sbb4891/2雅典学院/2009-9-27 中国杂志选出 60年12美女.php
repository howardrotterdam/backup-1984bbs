													<h2>O果日螅汗阒荨赌戏饺宋镏芸》选出60年来最美女性</h2>
						
						
																											<div id="postmessage_260148" class="t_msgfont">2009年09月25日 <br />
【建政 60周年倒数 6日】中共建政 60周年临近，广州《南方人物周刊》昨推出「 60年最美 12位女性」，如同五年前该刊评出「影响中国公共知识分子 50人」一样，这一举动令人惊讶，更令人佩服。<br />
<br />
<img src="http://us.f6.yahoofs.com/hkblog/4KYsljeZFRsQdQNJYxPdzEQ_s0QFrvi__DOT__yqSZuXU-_2/blog/20090927014256107.jpg?ib_____DP3u3qeBw" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
没有必要揣测，有关评选是基於甚麽动机；也无必要猜度，内地主管宣传的当局会不会如五年前那样，出手「收拾」这家「敏感时期再添乱」的刊物。外界对这 12位美人有兴趣，看她们有人美得如此灿烂，有人美得如此凄惨，不免掩卷深思！评选者称， 12位美人中，林徽因虽然在中共体制内只生活了几年，但她「对中华人民共和国国徽设计的贡献」是她入选的原因。她的早逝，使她避开了後来的政治灾难，没有像她的丈夫梁思成那样，在批斗和羞辱中死去。　本报记者<br />
<br />
60年来最美女性<br />
林徽因（ 1904年－ 1955年）<br />
美与智慧的绝唱<br />
<img src="http://us.f6.yahoofs.com/hkblog/4KYsljeZFRsQdQNJYxPdzEQ_s0QFrvi__DOT__yqSZuXU-_2/blog/20090927014114626.jpg?TTA4UwKBHHq9OrI_" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
被列为 60年最美女性之首。评选者用大量文字，描述这位生於清朝末年丶主要生活於民国时代的才女。作为上世纪初期女性，林徽因多才多艺，与近代众多学者名人齐名，在中国近代史上堪称罕见，被形容为「美与智慧的绝唱」。<br />
<br />
林徽因祖籍福建，生於杭州，丈夫为近代建筑师梁思成。她早年与夫同赴美留学，专攻艺术，演过戏剧，写过诗歌丶小说丶散文等，多才多艺，气质如兰，一度迷倒大诗人徐志摩，与闻一多丶梁实秋丶沈从文等相交颇深。<br />
<br />
中共建政後，她受聘清华大学建筑系教授，并与丈夫一道被选为设计中华人民共和国国徽。她 1955年因肺癌病逝，当时年仅 51岁。虽然早逝，但却幸运地躲过了後来的文革，没像丈夫梁思成那样受到屈辱。 <br />
<br />
严凤英（ 1930年－ 1968年）<br />
自杀後惨遭剖腹<br />
<img src="http://us.f6.yahoofs.com/hkblog/4KYsljeZFRsQdQNJYxPdzEQ_s0QFrvi__DOT__yqSZuXU-_2/blog/20090927014142652.jpg?TTA4UwKBQBBt3L3X" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
又一位因美丽而被文革不容，死於非命的女性。评选者以沉痛的笔触，描写严凤英被迫害自杀身亡後，遭军方下令剖腹探查腹中是否藏有特务工具，令人不忍卒读。<br />
<br />
严凤英出生安徽桐城。她 10岁开始学唱黄梅戏，小小年纪即唱红戏坛； 1954年因演出《天仙配》获一等奖，令她成为黄梅戏的领军人物，《天仙配》创造了当时戏曲影片上座的最高纪录，更成为内地经久唱不衰的名曲。<br />
<br />
但她在文革时因饱受迫害而自杀身亡，年仅 38岁。她当时被人举报为特务。她死後，军方以寻找「特务发报机」为由，割开她的喉管，挖出内脏，後人描述当时的情况「医生用手术斧头从咽下砍起，一根肋骨一根肋骨砍，把内脏拉出剖开，寻找特务工具，但一无所获。」 <br />
<br />
章含之（ 1935年－ 2008年）<br />
上海最後的名媛<br />
<img src="http://us.f6.yahoofs.com/hkblog/4KYsljeZFRsQdQNJYxPdzEQ_s0QFrvi__DOT__yqSZuXU-_2/blog/20090927014159932.jpg?ib_____DV2tfYbww" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
章含之是入选女性中唯一曾走红中共政坛的名人，评选方以「上海最後一位名媛」来表达她入选的原因；对她生平的介绍，也主要限於她的名门出身，以及她在中共外交官场的经历。<br />
章含之出生於上海，是着名民主人士章士钊的养女。她毕业於北京外国语学院，因为出身名门， 1963年获选为毛泽东的英文教师。後来，在毛泽东教唆下，她与任职教师的丈夫洪君彦离婚，再嫁给时任外交部长乔冠华，并投身外交界。<br />
<br />
上世纪 70年代初，中国外交部有「五朵金花」，分别是章含之丶王海容（毛泽东的表侄女）丶唐闻生（民国名人唐明照之女）丶齐宗华和罗旭。章含之参与了中美建交会谈丶尼克逊访华等一系列重大外交活动，官至外交部副司长。 <br />
<br />
潘虹（ 1954年生；现年 55岁）<br />
演艺圈最後贵族<br />
<img src="http://us.f6.yahoofs.com/hkblog/4KYsljeZFRsQdQNJYxPdzEQ_s0QFrvi__DOT__yqSZuXU-_2/blog/20090927014206562.jpg?TTA4UwKBQvD1ENdy" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
作为上世纪 80年代初期的电影红星，潘虹先後演出过 50多部影视剧，获得近 20个影视奖项，她演出的电影中，尤以 1989年的《最後的贵族》最受外界关注。美国《时代》（ Time）周刊曾以她作封面人物，她是继邓小平後，中国第二位上《时代》封面的人物。<br />
<br />
潘虹身世复杂，据说祖先有俄罗斯血统，而她那双大大的眼睛传递出一种高贵的忧郁，曾令法国影坛大师尚─卢．高达（ Jean-Luc Godard）大为赞叹，曾忽发奇想，要这位东方女演员饰演荷李活最神秘的影星嘉宾。<br />
<br />
评审视潘虹为已经过气演艺圈的最後贵族来看待，指她虽然老了，但依然气质卓然丶冷傲并耽於内心。「她属於平民贵族，只是，我们已经渐渐进入了一个没有贵族的时代。」 <br />
<br />
巩俐（ 1965年生；现年 44岁）<br />
向世界输出美丽<br />
<img src="http://us.f6.yahoofs.com/hkblog/4KYsljeZFRsQdQNJYxPdzEQ_s0QFrvi__DOT__yqSZuXU-_2/blog/20090927014222581.jpg?TTA4UwKBgDz8Aldr" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
「她曾站在中国影坛的巅o，她是西方人眼中最美的东方女人，时光流逝，她的光芒非但没有褪色，反而更加夺目耀眼。」这是评选方对这位大美人的评价，也代表了巩俐入选的理由。<br />
<br />
祖籍山东的巩俐，在中国东北出生。她在中央戏剧学院毕业後，被导演张艺谋选中，出演电影《红高梁》的女主角，自此一炮而红。她先後演出过 20多部影片，夺多项国际大奖，成为国际级影星，被媒体誉为「中国向世界输出的美丽形象的代表」。 <br />
<br />
伏明霞（ 1978年生；现年 31岁）<br />
幸福的跳水女皇<br />
<img src="http://us.f6.yahoofs.com/hkblog/4KYsljeZFRsQdQNJYxPdzEQ_s0QFrvi__DOT__yqSZuXU-_2/blog/20090927014248216.jpg?TTA4UwKBqkllyfnO" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
作为「香港媳妇」的伏明霞，港人对她的认识应该不逊於内地。评选方只用了一句「幸福跳水女皇」来形容她，相信与她退役後嫁入香港豪门，成为衣食无忧的贵妇有关。<br />
伏明霞是湖北人， 9岁就进入湖北跳水队训练， 14岁就获得首枚奥运会金牌，之後又接二连三夺冠，成为中国跳水队拥有最多奥运奖牌的选手（四金一银）。 2002年她下嫁比她年长 26岁的时任香港政府财政司司长梁锦松，完成了她人生又一个「惊人的一跳」。 <br />
<br />
王丹凤（ 1925年生；现年 84岁）<br />
美丽不止 60年<br />
<img src="http://us.f6.yahoofs.com/hkblog/4KYsljeZFRsQdQNJYxPdzEQ_s0QFrvi__DOT__yqSZuXU-_2/blog/20090927014123506.jpg?ib_____D4AewplTr" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
被喻为绝代佳人的王丹凤，评选者对她的外表极尽赞美，指「她那张脸是标准美人的典范。这样的脸，走到那里，那里就春暖花开」，「五官端正丶樱桃小嘴丶杨柳细腰，迷倒无数男人」，「标准美人典范，美丽不止 60年」。<br />
<br />
王丹凤祖籍宁波，出生在上海， 16岁踏入电影界，从影 60多年，主演了近 60部影片。 1957年，正是大跃进运动的红火之年，她主演电影《护士日记》，创造了一个可爱女护士形象，电影上演後，许多男人找女朋友，都首选护士；而片中那首女护士哄小孩入睡的插曲「小燕子，穿花衣，年年春天来这里」，迄今仍在内地传唱。<br />
<br />
在文革同样遭冲击的她，於 1980年以 55岁之龄主演最後一部电影《玉色蝴蝶》，扮演日本少妇，此後息影。 1992年，她随夫来港定居，开办素食餐馆「功德林」。 <br />
<br />
张志新（ 1930年－ 1975年）<br />
勇敢地反抗中共<br />
<img src="http://us.f6.yahoofs.com/hkblog/4KYsljeZFRsQdQNJYxPdzEQ_s0QFrvi__DOT__yqSZuXU-_2/blog/20090927014151503.jpg?ib_____Drb9.4oWi" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
她是典型的政治人物，获选 60年最美的女性，是因为她在反抗中共当年黑暗政治的时候，表现出非凡的丶「死神也不能夺去的勇气和美丽」。对张志新的追忆，就意味着对历史的反省和控诉。<br />
<br />
原籍天津的张志新，本来只是辽宁省委宣传部一名普通干部，就因为她对毛泽东思想表示质疑，在文化大革命中被捕，坐了六年监牢，受尽酷刑。文革结束前，她被毛泽东的侄子丶时任辽宁省委书记的毛远新下令处以极刑。她临枪毙前，当局还割断她的喉管，防她喊口号。<br />
文革结束後，有媒体要调查追究张志新遭残酷迫害的内幕，但後来被时任中共总书记Hu Yaobang下令停止。评选者透露，她有两个孩子定居外国，很少回去，亲人相聚也绝少提起母亲，因为「那是一个从未结痂的伤疤」。 <br />
<br />
杨丽坤（ 1941年－ 2000年）<br />
文革令美丽凋零<br />
<img src="http://us.f6.yahoofs.com/hkblog/4KYsljeZFRsQdQNJYxPdzEQ_s0QFrvi__DOT__yqSZuXU-_2/blog/20090927014134779.jpg?ib_____D_ryQ5ooz" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
「她的美丽因（电影）《阿诗玛》广为人知，却因政治运动迅速凋零。」这是评选者对杨丽坤当选 60年最美丽女性开宗明义的评价，显示她的入选与政治不无关系，成为对中共 60年丑恶政治运动的有力控诉。<br />
<br />
本身是彝族的杨丽坤生於云南普洱县， 15岁开始跳舞， 18岁主演反映彝族爱情生活的电影《五朵金花》轰动一时，她扮演少女形象迷倒无数影迷。该片在文革中被指是「资产阶级情调」，停止发行，她本人也因此被批斗，导致精神失常。<br />
<br />
文革後，她虽获治疗病情好转，但终因长期病痛折磨和身体机能衰退， 59岁即去世。评选者以「悲情阿诗玛」作为对杨丽坤人生的总结，并以很大篇幅，对她在文革中的遭遇作详细描述，令人读之，扼腕长叹。 <br />
<br />
周晓兰（ 1957年生；现年 52岁）<br />
中国女排一枝花<br />
<img src="http://us.f6.yahoofs.com/hkblog/4KYsljeZFRsQdQNJYxPdzEQ_s0QFrvi__DOT__yqSZuXU-_2/blog/20090927014213586.jpg?ib_____DChokSKGN" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
上世纪 80年代，「中国女排」是时下流行语，因为中国女排首次赢得排球项目的奥运会冠军。周晓兰就是这群姑娘中的一位，当时她以出色的拦网，频频将对手的凌厉攻杀挡回，被体育评论员誉为「天安门城墙」。<br />
<br />
出生於江苏南京，身高 1.82米的周晓兰，与体坛强手通常都长得「五大三粗」不同，她不但个子高，长相也堪称秀气，被公认为当时中国女排的一枝花，内地当年最畅销的杂志《大众电影》也一度以她做封面。<br />
<br />
周晓兰代表了一种特定的美：为国争光。她退役後曾官至国家体委副司长。遗憾的是， 1995年，这位代表了中国女性强与美最佳结合的前世界冠军，突然辞官，跟随丈夫赴美国定居，令无数国人感叹。周晓兰现育有两女，她希望能将女儿送回中国，学习中文。 <br />
<br />
杨澜（ 1968年生；现年 41岁）<br />
职业女性的典范<br />
<img src="http://us.f6.yahoofs.com/hkblog/4KYsljeZFRsQdQNJYxPdzEQ_s0QFrvi__DOT__yqSZuXU-_2/blog/20090927014231180.jpg?ib_____D0Sd.Md8v" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
「我不是很漂亮，但我很有气质。」这是杨澜对自己的评价，而评选方对她的点评是：「她是知性丶大气丶得体等一系列形容女性褒义词的最好代言人。她是职业女性的典范，永远优雅，永远出色，永远先行一步。」<br />
<br />
杨澜是上海人，大学是外语专业。她先在中央电视台任主播，後赴美留学丶工作，之後创办阳光o视。她制作和主持的《杨澜工作室》曾先後访问许多中外政坛和商界的名人。由於英文了得，她先後在北京奥运等重要场合担当主持，为同行称道。她还在今年 7月，与流行天后 Celine Dion合作打造中国首家高级定制珠宝店，品牌命名为「 LAN」。 <br />
<br />
章子怡（ 1979年生；现年 30岁）<br />
拥有独特东方美<br />
<img src="http://us.f6.yahoofs.com/hkblog/4KYsljeZFRsQdQNJYxPdzEQ_s0QFrvi__DOT__yqSZuXU-_2/blog/20090927014239209.jpg?ib_____DZxNSqOcg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
章子怡以独特东方美女面孔，被形容「清纯似小溪，冷艳如大海。」她从 1998年出道，直至成为国际巨星，她的成功被归纳成三点：漂亮，不一般的漂亮；机会，不一般的机会；用功，不一般的用功。与她合作过的大导演，对她都赞不绝口。<br />
<br />
出生於北京的她， 8岁学跳舞， 17岁考入中央戏剧学院表演系， 1998年担任张艺谋导演的《我的父亲母亲》女主角而走红，其後拍过《卧虎藏龙》丶《英雄》等。她被张艺谋指是近年世界影坛上获认同的中国女星中，地位最高的其中一名佼佼者。<br />
