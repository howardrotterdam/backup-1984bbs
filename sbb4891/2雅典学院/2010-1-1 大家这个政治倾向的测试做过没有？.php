在这个测试题中，有几个关于中国传统文化的题目，这里用上的就是这个意义上的左右观，但是，很奇妙的是，在这个测试题中，恰恰颠倒了左右的含义，即把更相信传统价值的定为了左，而将更相信自由、个体、开放的定为了右，这是为什么呢？原来，他所说的左右是1949年中国语境下的左右，大致分三个阶段： <br />
<br />
<br />
<br />
阶段1：1949-1979：支持社会主义改造等官方路线的为左 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;质疑乃至反对的为优 <br />
<br />
阶段2：1978-1949：在这一阶段，此前的官方路线已经在经济和社会上出现了很大变化， <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; 但是，官方仍坚持其意识形态和政治体制，在这个时候，坚持特定政治体制的为左 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; 反对者也就是是资产阶级自由化者为右，这批人大多持自由市场、市民社会、有限政府，民主选举等立场。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;1978至今：在这一阶段，官方明确推行自由市场和对外开放，这一来，左的含义主要集中在了反对市场经济和全球化的立场上。而随着官方的传统意识形态的消解，民族主义立场（复兴）开始进入左的谱系。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;右派则大体沿袭了80年代的立场。而有新的变化。（见下） <br />
<br />
<br />
<br />
结合这套题，可以看出，题目设计者在政治上，将赞成个人权利、制约政府权力、赞同学习西方民主、赞同改革现行政治体制和赞同程序正义设定为右，反之为左。将赞成更多市场、更少干预，更少福利、更多经济开放以及更多地支持特定改革进程设定为右，反之为左。而在文化上，将质疑国家主权至上，质疑集体道德、质疑传统文化设定为右，反之为左。 <br />
<br />
<br />
<br />
上面已经讲过，中国当代语境下的右派主要体现为现行体制的反对，因此，在一开始其一致性相对较强，即就是坚持自由市场、市民社会、有限政府、，民主选举、普世价值,但是，中国的改革带来了特定的进程和后果，而中国的开放也重塑了中国与世界的关系，因此，在针对中国特定改革进程、社会福利和经济民族主义等问题上，原本一致的右派开始出现不一致，并不是什么不可理解的事情，因此，体现在这套题的测试中，大概会出现这样的情况： <br />
<br />
<br />
<br />
政治右派将会在前三个问题和程序正义上取得相当一致，所以，右派在政治上出现1以上的分数相当普遍，不出人意料。 <br />
<br />
但是，政治右派会在政府权力、自由市场、对中国特定改革过程以及经济民族主义上出现一定分歧，这会导致即使政治上相对一致的右派在经济上的分值悬殊。 <br />
<br />
同时，政治右派在集体道德上得分一致，但在国家主权问题上会呈现落差，在传统文化问题上也有落差，这使得泛右派的文化分差相当悬殊。 <br />
<br />
<br />
<br />
<br />
<br />
反之，中国现在的左派大致是上述三阶段左派的一个合集，既包括支持文革的毛左，又包括无论如何赞同新中国法统也就是坚持政治稳定的人，同时还包括反思市场价值和全球化的新左派，同时还包括民族主义者，以及文化保守主义者。表面上看，上述所有这些倾向之间，是有着冲突的，他们之所以被看作一个派别，是因为他们或多或少反对其对立面也就是坚持自由市场、市民社会、有限政府、，民主选举、普世价值。。。。。。。的右派。但是，以我的观察，这一合集有越来越交迭的迹象，即体现在坚持现行政治体制、坚持国家主权至上和坚持平民主义上，正有合流的趋势。因此，体现在这套题的测试中，大概会出现这样的情况： <br />
<br />
<br />
<br />
<br />
<br />
1、政治左派的分值绝对值不会太高，但得分比较一致，这是因为，即使是左派，也开始对国家权力保持警惕，对个人权利有所坚持，并对程序正义的法治原则有所赞同。 <br />
<br />
2、经济左派的得分也不会高，不一致主要来自于这套题目中对中国特殊的改革问题的测试题目，这可以有效测试出全盘反对改革的极左，但对于那些即使坚持现行政治体制、坚持国家主权至上和坚持平民主义的左派来说，对中国特定的改革过程也很难持全面反对的态度。 <br />
<br />
3、文化左派的得分会更不一致，如前所述，即使坚持坚持现行政治体制、坚持国家主权至上和坚持平民主义的左派，也对个人权利有所坚持，对程序正义基本赞同，这使得他们与文化保守主义的左派在集体道德和传统文化上拉开距离。。 <br />
<br />
<br />
<br />
结合我最开始的讨论，可以发现，这套试题用于测试针对特定政治制度的右派和右派，效果都比较好，都确实会出现一个分值较高的可见群体，其一致性相对较好。但是，在经济上，由于社会福利和对中国特定改革进程的判断不一致，右派的一致性会损失，而在国家主权问题、经济民族主义、传统文化问题上，一致性继续在消失，尽管在集体道德问题上，右派是相对一致的。 <br />
<br />
<br />
<br />
如果真是这样，中国的泛右派的交集应该是强调个人权利、限制公共权力、支持民主选举、赞成市场经济、主张道德自主。。。。。。而泛右派在社会福利、经济民族主义、国家主权、传统文化上会出现离散。 <br />
<br />
<br />
<br />
反之，左派的交集大概会集中在在坚持现行政治体制、坚持国家主权至上和坚持平民主义上，在社会福利、民主选举、中国特殊政治问题、国家主权、经济民族主义上一致，但泛左派会在集体道德上、中国特殊的改革过程和传统文化上会产生分歧和离散。 <br />
<br />
<br />
<br />
<br />
<br />
因此，当代中国的左右分歧大概可以归纳为右派由共同的交集在分化扩散，而左派在合集的碰撞中逐渐浮现出交集。 <br />
<br />
<br />
<br />
如果真是这样，这套题的问题也就很清楚了，导致右派离散的问题包括社会福利、中国特定改革进程、国家主权、经济民族主义、传统文化，而导致左派离散的问题包括中国特定改革进程、集体道德和传统文化，两者共同的交集是传统文化和中国特定改革进程，如果将这两个方面的问题剔除，应该会极大地提高这套题在当代中国语境下的左右鉴别能力。</div>

							
							
							
							
