																											<div id="postmessage_827393" class="t_msgfont">看看这个你就知道为什么中共不敢和小日本叫板了<br />
打印版 【 阿波罗新闻网2010-09-12讯】&nbsp;&nbsp;<br />
　<br />
说起轴承，喜欢军事的人都知道，这是战略物资，旋转的机器都要用到它。二战时，英美盟军的战略空军专门轰炸过德国的轴承工厂。　　<br />
<br />
中国著名的轴承生产厂家是“哈瓦洛”三家，哈尔滨，瓦房店，洛阳。中国现在的造船业发展得异常迅猛，可以用惊世骇俗来形容，船舶上使用轴承量那是很大的。<br />
<br />
但是，了解造船工业的人都知道，远洋船舶，那是从来不用国产轴承的，全部进口，日本，德国，法国等等，远洋船舶肯定不会使用中国产轴承，中国那些航运公司的老总即使再混蛋，他也不敢拿船舶的生死安全开玩笑。<br />
<br />
现在国内生产的豪华汽车很多，我估计不会使用进口轴承吧，别看你那豪华汽车贼牛比，还不如船舶上一台小水泵的装备牛皮呢。<br />
<br />
还是造船业，了解的人都知道，我们的造船业也就是造个“皮”，就造个船壳子，基本上是焊焊钢板，其实很多出口船舶的钢板都是由外国船东指定进口的。船舶里面的设备，包括水泵这种超低级产品都是要进口的，主要进口于日本等国。<br />
<br />
这里的“进口”有两种意思，一是纯粹的从国外进口设备，以这类为多。二是西方企业在华生产，或者中国企业购买许可证生产，完全为外国牌子打工。<br />
<br />
许可证生产就行了吗？也不行，比如我们的工厂完全进口西方某一产品的零部件，运回来自己组装，结果组装出来的产品性能就是不如西方人自己生产出来的，真是没办法。<br />
<br />
所以，中国的装备制造业，倒闭的倒闭，合资的合资，被收购的收购，剩下的靠购买西方专利和技术以苟且偷生，有时也竟然敢不知羞耻的吹吹牛。<br />
<br />
以上说了这么多是为了什么？别看中国牛皮烘烘，自诩世界工厂，自吹要崛起，其实啊，崛起最基本的资本――装备制造产业，中国和世界强国相比较，实在是惨不忍睹，牛什么呢？<br />
<br />
中国的制造业为什么这样烂？我们中国人并不笨啊。<br />
<br />
装备制造业完全不同于做衣服搞玩具，这是个粗活，却更是个需要长期琢磨和探索的产业，更是一个来不得半点浮躁的产业，一句话概括：你得把它当“持久战”来玩，也许一辈子都玩不好，但是一个大国持之以恒以至几十年，肯定能玩好它，比如日本德国北欧等国家。<br />
<br />
问题在于：中国不合理的体制和现状以及世俗对工人的歧视造成中国的装备制造业注定要玩完。<br />
<br />
你一个产品，哪怕是一辆自行车，在目前的环境下，你如何保证所有的零部件及材料的采购都能合格？答案是绝对不可能，这中间的每一个环节都是黑幕重重。所以三十年前中国造的自行车现在也许还可以骑，三年前中国造的自行车现在可能就不能骑了，没办法，神仙也救不了。<br />
<br />
装备制造业一般都是公对公，就是公家生产，再卖给公家，没办法，想不腐败都不行，大家做生意不是靠质优价廉，是靠质劣回扣多。<br />
<br />
我们中国自古以来就是士农工商挨个排序，农民就不提了，至于工人，现在谁还想做工人？哪个女孩子愿意嫁给工人？以前有些工人的收入不比你公务员低，包括万恶的旧社会时期，从全世界范围来看，技术工人并不比你那白领和公务员拿的钱少。<br />
<br />
现在的中国呢？一个技术含量响当当的熟练工人，干了几十年之后，其收入基本上远远不如刚进政府机构屁都不会的傻学生，而且差的还不是一个两个量级。人为财死鸟为食亡，这样的收入，鬼才想做工人。<br />
<br />
大家应该听说过，西方一些国家，工匠那是代代相传的，爷爷用的工具，到了孙子辈还留着使用，还当宝贝，还以此为荣耀，那说出来是相当拉风的，中国以前的工匠也有传代的。现在呢？哪个做老爸的会把这样的“粗活”传给儿子？简直是有辱门风，脑子有病，这就是中国技工的社会地位，真是屁都不如。<br />
<br />
你高级技工怎么啦？下岗呗，领救济金呗，你说你很牛皮，咋就找不到高收入的工作？对了，你即使技术一流还就是常常不好找工作，因为我们国家真不需要。<br />
<br />
为什么不需要熟练技术工人？我们的制造业现在是以农民工为主，以大量生产劣质产品为目的，中国的制造企业没有谁想着去搞科研玩技术，买个专利混一天是一天，那些老总们谁知道明天要到哪里当官去了？农民工干活就足够使用了。<br />
<br />
中国动不动就吹嘘又有啥方面达到国际水平了，其实啊，要么是直接诈骗，要么是盗版人家的技术和产品，然后修改一下数据，比如进口一辆自行车，把车轮子放大一点，再自己造辆出来，质量差得根本不能用，这没关系，照样吹嘘是世界先进产品了。<br />
<br />
想叫农民工学技术？那是脑子有病，农民工连生存于城市的资格都没有，他们今天干这种活，明天都不知道还能干啥工作，他凭什么要去学技术？他拿什么去学技术？他学到技术又能有什么用？<br />
<br />
这样的工人，这样的环境，这样的管理，那么一个产品的制造过程中，材料，工艺，采购，制造，销售，维护，每一个环节都存在大量的致命的问题，那么中国企业制造出来的产品如何保证质量？<br />
<br />
江南“神盾”舰让广大爱蝈小干将HIGH到死，不过随即中国又制造低端的军舰了，有了神盾干嘛又搞俄式老军舰呢？我估计啊，神盾虽好，中看不中用。四川震灾，为什么看不到国产直升机？权威官方媒体公开说了：国产直升机实战中难堪大用，意思说白了就是不能实战，还不明白吗？<br />
<br />
一个产品，要好用，要耐用，更要好维护，你一艘非常先进的军舰，如果动一动就趴窝，就出问题，如果老牛上场到处毛病，那即使再先进也就是一摆设，印度尼西亚也买过德国的先进潜水艇，德国的产品肯定是保证质量的，但是印尼人不会用，印尼人没本事正常使用和正常维修保养潜水艇，所以那么昂贵的东西就趴在港口里报废了。<br />
<br />
