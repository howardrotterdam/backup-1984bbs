																											<div id="postmessage_520909" class="t_msgfont">尽管不是专业的妇产科医生，但是作为一个医生，我对楼主这种不负责任，断章取义的文章表示谴责。<br />
首先，国内剖宫产比例比国外高，很大程度上是由病人而非医生决定的。所有医生都知道顺产无论对母亲，还是对婴儿都比较有好处，而且便于生二胎恢复，但是由于许多病人对顺产怀有恐惧，担心一旦顺产不成还是要转开刀（这也容易理解，多数人都是第一次生孩子），在国内医疗环境下，医生一般尊重患者意愿（谁愿意弄两次多花时间，直接剖出来也方便），所以造成国内剖腹产比例较高。另外还有一个原因，国外妇女大多数需要再次生育，反复剖宫产会造成疤痕子宫，若胎儿着床在疤痕部位，容易造成危险，甚至子宫破裂（尽管是小概率事件）。<br />
