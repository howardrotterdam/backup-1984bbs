&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　然而，那个年头的头号强国听不懂或不愿听懂这个“言之有预”，根本就没有把尚在襁褓中的新中国放在眼里，仍然以“第二次世界大战时那个软弱的国家”来认识这个古老土地上的新生国家，认定了欺侮了你也是白欺侮。这个时候，除了掂枪挟炮起而反抗侵略，还能拿什么东西与他们平等对话平等交流？而“抗美援朝，保家卫国”这个口号之所以在中国人民中具有毫不含糊毋庸置疑的号召力，正是因为中国人民从并不久远的历史教训中就能很方便地比较鉴别：这直奔家门而来的客人，究竟是好心，还是歹意？<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; “敢战方能言和”<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　面对当年霸权肆虐的世界，要想赢得和平生存和平发展的环境，敢战方能言和，和平的愿望未必能带来和平，展现你维护和平的力量才能争取真正的和平。对于掌控着国际话语权的帝国主义霸权主义，更深层次的了解和交汇恰恰是交手过招之后的成果！<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　在中国人民反抗外来侵略的斗争历史上，只有新中国，第一次在真正意义上拒外来威胁于国门之外，遏侵略凶焰于初临之时；只有新中国，第一次基本依靠自己的力量，卓有成效地维护了我们和平生存、和平建设与和平发展的权利；只有新中国，第一次出色地履行了对盟友乃至对世界和平的国际主义义务，鲜明地向朋友，向敌人，向世界，展现了“一个负责任的大国”真真切切的现实存在：言必信，行必果；不抛弃，不放弃；敢承受，能担当！而以世界头号强国为首的列强们，也正是通过这场战争的较量和交往，开始真正听懂了他们曾经听不懂的铿锵之声：<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　“中国人说话是算数的！”<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　“中国人民已经组织起来了，是惹不得的。如果惹翻了，是不好办的！”<br />
&nbsp; &nbsp; <br />
