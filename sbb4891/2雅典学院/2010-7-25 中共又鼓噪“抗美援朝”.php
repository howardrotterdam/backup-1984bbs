&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;一战扬威，稳定家国半世纪；百年雪耻，震慑对手数十年 (博讯 boxun.com)<br />
<br />
&nbsp; &nbsp; 文/双石 <br />
&nbsp; &nbsp;&nbsp;&nbsp;来源：《t望新闻周刊》 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;六十年前的“抗美援朝”，无疑是新中国历史上一座重要里程碑。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　那年那月，这个从持续了百年之久的干戈离乱中诞生的新生国家刚满周岁，当她来到这个世界上时，迎接她的，是断墙残垣，是瓦砾废墟，是荒芜已久亟待耕耘的土地，是千千万万嗷嗷待哺的生灵！还有欲待重整的残破河山，渴盼归一的边远疆域，切望荡平的残敌悍匪，急需洗涤的污泥浊水……<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　她对和平生活和平建设的向往，如同久旱的土地之望云霓。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　然而，她却毅然决然地派出了她的百万优秀儿女，肩荷着“万国牌”武器，昂首挺胸地走出国门，迎着扑面而来的硝烟烈火、枪林弹雨，去迎击以世界头号强国为首、自称“联合国军”的铁甲劲旅！以至于当年的对手和许多朋友都认为这是多么地不可思议：家园百废待兴，自顾尚且不暇，又何以兵临险境，勉为其难地以卵击石？即使在今天，仍有人喋喋不休地以战争中的每一项得失来斤斤计较：当年新中国领导人这一惊世骇俗的重拳出击是否必要？利害相较，是得分还是失算？<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　出兵援助有前提负责任<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　审视历史需要有穿透历史的眼光，何况历史演进的本身就能说明一切！无须对此作太久远的回顾，日本军国主义霸占朝鲜而后染指中国，从而使中朝两国先后沦为殖民地或半殖民地的惨痛教训，对两国人民就足具史鉴来者之功效――“唇亡齿寒，户破堂危”，何过于此？而唯其如此，早在第二次世界大战前后，两国爱国志士就携手并肩互相支援相濡以沫共挽民族危亡。而二战结束后，中国人民革命斗争在短短四年间取得了决定性胜利，并且继续以磅礴之势推进百年来首次在真正意义上完成民族统一之大业，这自然而然地鼓舞了朝鲜人民完成祖国统一的决心和意志。而支持朝鲜人民正义斗争，对于正进行同样事业的中国人民来说，毫无疑问也是天经地义的。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　然而支持并不等同于直接介入，建国伊始，新中国自己的问题一大堆，清匪反霸，土地改革，巩固政权，恢复经济，解放台湾，进军西藏，都决定了新中国的战略重点应该是在南而不是在北。百废待兴之时，新中国的领导人和人民群众都不愿意与任何一个国家之间发生战争。新中国对于兄弟邻邦的支持只能基于道义的声援和有限度的物资支援，同时必须作危急情况下的应对准备。而新中国领导人对友好邻邦作出的在紧急情况下出兵援助的承诺也是有前提责任的――美国军队“不过‘三八线’，我们不管；过了‘三八线’，我们一定打过去”。这是明确的宣示：“如果不是美国侵略了朝鲜民主主义人民共和国和我国台湾，打到了我国的东北边疆，中国人民是不会与美国作战的。”<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　而世界头号强国偏偏就冒了这个天下之大不韪。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　曾任美国国务卿的亨利・基辛格也曾经婉转地承认：“毛泽东有理由认为，如果他不在朝鲜阻挡美国，他或许会在中国领土上与美军交战。最起码，他没有理由去作出相反的结论。”<br />
&nbsp; &nbsp; <br />
