													<h2>共军败绩</h2>
						
						
																											<div id="postmessage_571177" class="t_msgfont">党军的不可战胜，就好像封神演义里诸神祭在空中的法宝一般，是亲共左爷们念念不忘的口头禅。冷眼看去，不仅让人想起电影里高喊“刀枪不入”的神兵和一百年前盲目自大的愚昧拳民。 <br />
<br />
　　也不应该全怪他们。在中国，历史就象块橡皮泥，当朝天子捏成什么样，就是什么样。本朝更是到了空前，希望是绝后，的地步。动不动用兵如神，战无不胜。当党大人们把这些垃圾写进教科书，灌输到下一代的脑里以后，一批又一批党忠实的信徒就在这谎言里茁壮成长起来，祖国江山不代代红才怪了。 <br />
<br />
　　可惜，就算在独裁专政的中国，吹牛皮虽然不上税，吹破的时候总是有的。谎言终究不能永远招摇撞骗下去。 <br />
<br />
　　共产党不是什么特殊材料制成的神。共产党的发家历程就是一部夹缝中生存，死里逃生的经历。红军时代就不用说了，如果胜多败少，就不会被国民党打得东奔西跑，到处流窜了。象什么西路军，方志敏的红10军团这些全军覆没的失败都没什么提的必要，被打得满地找牙的时候多了。当然，在伟光正的历史里，总有的左或者右的替罪羊不断出现，这些“挫折”和战无不胜的武功毫不相关。尽管当时如果没有西安事变，能不能在陕北那山沟里活下来恐怕都是问题。 <br />
<br />
　　八年抗战，当国民党军在前线以弱抗强，流血牺牲的时候，共产党从三，四万人发展到百万大军。从电影到媒介，我们每天都看到李向阳式的英雄，听到从胜利走向胜利的回忆。HOWEVER，真叫起真来，就会发现这号称“抗击了60%侵华日军，90%伪军”的党军武装居然没有一次全歼日军大队以上建制的记录。所谓的平行关战斗，不过是以优势兵力攻击日军数百人的辎重队。2万5千里长征后的老骨头去打鬼子的运输队却打得艰苦无比，伤亡之惨重吓得老毛马上下命令禁止进行类似作战。好不容易3年后又来了次百团大战，作战数月，大小战斗1800余次，毙伤日军20645人，创造出平均每次作战歼敌(打死打伤)不过十人出头的有趣统计来。党的历史更从来没有提过，在这所谓“60%”党军所“抗击”的日军当中，居然没有一个甲级师团。日寇的全部甲级师团全部都部署在与国民党军作战的第一线。而且，每每日军要在正面战场发动攻势时，兵力集结，物资准备也从没有因为八路的存在而失败过。日军的军队，给养都援源不断从东北调到华北，进到华中前线与国军作战。日军无不讽刺地认为：“就战斗力而言，八路军是一支土匪武装。”党军的战斗力果真如此吗？打自己人却不手软。共产党吞并山西新军，河北民军等抗日武装，以反顽为名攻击各路友军的时候，可厉害着呢。新四军抗战过程中最有名的战役居然是打自己友军韩德勤部国军的黄桥战役。在黄桥后新四军攻击韩部的曹甸战役中，日寇竟然都在配合其行动。 <br />
<br />
　　不过，打日本没出力，借国难扩展势力，下山摘桃子是不道德，打不过日本也有理由，国军不也一样打不过日本嘛。 <br />
<br />
　　抗战胜利，国共开战。党军从国民革命军再次改名成为“解放军”，在本朝的记录里再次充满从胜利走向胜利的辉煌。小米加步枪打败武装到牙齿的八百万蒋匪的事迹成了大陆老小皆知的常识。虽然，国共抗战结束时的实力对比远没有那么悬殊。从人数上讲，45年时的430万国民党军包括了后勤部队，守备兵团。而仅120万共军后面却有民兵，民工，自愿也好，受愚弄也好，被强迫也好，反正是在帮共产党的。从武器上讲，共军小米加步枪，国军也好不过哪里去。所谓的美械部队听起来，看起来漂亮，不过是有些美械轻步兵装备而已，多数都是美国在国军打日本时给的，而且弹药消耗大，后勤要求高，在落后的中国远不如日械实用。国军不少美械部队，象所谓的快速纵队，都是在人海战术围攻下耗光了弹药玩完的。再加上美国不支持老蒋内战，弹药，后勤供给严重不足，国军美械的威力大打折扣，以至於党军在朝鲜的真正美械面前才真正知道了厉害。从与老百姓的关系上看，国民党正规军的纪律就算不比党军强，也不差很多。国军是政府军，不象党军那样，走一处，吃一处，“如若蝗虫”。党军所谓的秋毫无犯，就是白吃白喝以后给老百姓党军自己印的钱票子，打借条，变相地去抢。“不仅收借条的人不知什么时候可以税现，连写条子的都不知道。”老百姓甚至有等到80年代才能拿到党军当年“借”的布粮钱的趣事。大别山就有商人在80年代以当年解放军开的借条作为革命文物献给政府，而被“人民”政府“按现价”付帐“奖励”的事。 <br />
<br />
　　不过，尽管共产党把自己当时的弱小夸张了不小，内战开始时，国军优势明显。党军被“战胜”的记录也就多了。在东北，仅46年，从现在出版物上就可以找到的大战斗就有： <br />
<br />
　　沙岭，党军以3个纵队攻击国军新6军一团，死伤数千，还不能把国军消灭；蚰岩，整个辽南独立师遭国军52军歼灭性打击，不得不逃到老毛子管的关东州求老毛子给以庇护；威远门堡，新6军以一团击败共军3纵一个纵队，除第一次试探性攻击以外，所有冲锋均一次得手；摩天岭，共军占据险要，却被国军巧妙突破，伤亡近万，一个纵队残废。通化，共军主力精锐原新四军3师7旅被国军195师重创。。。。。 <br />
<br />
　　这些，都是国军在兵力处於劣势的情况上打出来的。 <br />
<br />
　　同期在全国范围内共军失利的大战役可以找到： <br />
<br />
　　所谓的中原突围：都听说过英雄的皮旅，可皮旅本是掩护所谓主力突围的偏师，中原军区主力近6万人，尽数被国军打夸；二战四平：东北共军主力为国军击败，一溃千里，被国军追过松花江；大同，集宁，张北：两把菜刀和聂荣臻被傅作义迂回击溃，撤围大同，丢掉张家口，弃尸近万。“因恐怖国军追击，竟至拼命奔逃，口鼻冒血，倒身路旁著比比皆是”就是傅作义在“劝毛泽东结束战乱参加政府”的公开电中所描绘的共军惨状；淮阴：整74师半夜以小部队奇兵突破，打开淮阴城门，共军狼狈逃出，从此失去两淮，苏北(在“南征北战”这种共产党电影里还美其名曰战略转移。不是转移，而是被打跑了)；二战涟水：整74师把赶来救援的华野王必成6纵打残，一个万把人的纵队伤亡6千； <br />
<br />
　　巨野章缝集：中野被国军18军，5军击败，阵亡7纵19旅旅长吴大明在内大批干部，丧失了鲁西南根据地，晋冀鲁豫根据地变成了晋冀豫根据地，丧失鲁西重要的兵源、粮源、财源。 <br />
<br />
