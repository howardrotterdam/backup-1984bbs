刘晓波获奖 温家宝缄默被指错失良机<br />
<br />
中国异议人士刘晓波获得诺贝尔和平奖的消息公布后，近来不断谈及政治改革的温家宝总理一直没有做出直接或间接的回应。一些人士认为他错失了良机，没有趁在国外访问的机会对此有所表述。另一些人认为，指望温家宝回应刘晓波获奖是不现实的。<br />
<br />
挪威诺贝尔委员会星期五下午宣布刘晓波获得今年和平奖的时候，中国总理温家宝正在欧亚之行的最后一站土耳其访问。当天晚些时候，温家宝还与土耳其总理埃尔多安出席了双边合作签字仪式，并共同会见了记者。但是温家宝和埃尔多安没有回答记者的提问。两天前的星期三，温家宝在布鲁塞尔与欧盟举行高峰会之后，中方取消了预定的一个记者会，当时，刘晓波获诺贝尔和平奖的呼声很高。<br />
<br />
一些网民在博客上发贴，呼吁近来不断谈及政治改革的温家宝积极回应刘晓波获奖，但是温家宝已经在星期天回到了北京，并且一直没有对外直接或间接发表任何有关刘晓波获奖的评论，令不少人感到失望。<br />
<br />
*王光泽：温家宝错失良机推动政改*<br />
<br />
认为应该对温家宝最近有关政改言论给予支持和肯定的北京独立评论员王光泽说，温家宝没有在国外访问期间趁机积极回应刘晓波获奖令人感到惋惜，认为他错失了良机。<br />
<br />
王光泽说：“从中共对刘晓波的定性来看，中共把他定性为政治上的敌人，作为中共的核心人员和中国政府的总理，(温家宝)他不可能对刘晓波的案子有非常清晰的表述，但是他可以做的事情是什么呢？他可以就刘晓波这个案子再次强调中国人民追求民主、追求自由的步伐不会停止，他可以从侧面来回应，不方便正面回应的话，应该有个侧面的回应会比较好。我认为他丧失了这个机会。”<br />
<br />
一星期前，美国有线电视新闻网播出了在温家宝最近访问联合国期间对他的采访。温家宝在访问中说，人民对民主和自由的向往和需求是无法阻挡的，而他对政治体制改革“风雨无阻，致死方休”。在那之前，温家宝也几次谈到中国需要进行政治改革，否则经济改革的成果会得而复失。<br />
<br />
温家宝最近接二连三地发表有关政治改革与民主自由的言论鼓舞了许多渴望民主变革的中国人，也有一些人认为温家宝追求政治改革是假，维护一党专政是真。<br />
<br />
*孟浪：中央已做出决定 温家宝难以个人表态*<br />
<br />
居住在香港的独立中文笔会负责人之一孟浪说，外界不掌握中共高层内部的运作，很难判断温家宝的真假，因此现阶段指望温家宝回应刘晓波获奖是不现实的。<br />
<br />
孟浪说：“温家宝可以就一些笼统的政治改革话题做出目前看来是他带有个人色彩的表态，但是就这么一个特定的、敏感的话题，也就是刘晓波获得诺贝尔和平奖，温家宝是不是会做出一个表态，我觉得不会。因为，中国高层内部的政性或规范性已经制约他，他不可能单一地做出个人的表态。”<br />
<br />
自从刘晓波获得诺贝尔和平奖之后，中国当局明显加强对民主人士的打压，一些试图进行庆祝活动的异议人士被当局收押和遣返原居住地，一些经常接受境外媒体采访的人士这两天也跟外界失去了联系，他们的手机处于关机状态。一名家住深圳的异议人士在接通记者电话后说，刘晓波获奖后当局逼得很紧，因此他不能接受采访。<br />
<br />
*王光泽：温家宝需要拿出政改行动*<br />
<br />
尽管如此，北京独立评论员王光泽仍然对温家宝抱有期望，但他希望看到温家宝日后不要只停留在口头上谈政治改革。<br />
<br />
王光泽说：“我相信刘晓波获得诺贝尔和平奖是全体中国人的荣耀，我想它给温家宝的震动也是非常大的。我觉得温家宝应该有这个信心和力量去推动政改。推动政改的前提就是，我觉得温家宝必须有相对比较清晰的政治改革的愿景和蓝图。目前来讲，他只是表达意愿，没有愿景和蓝图，也没有实际行动，更没有和体制外的力量达成互动。”<br />
<br />
中国领导人还没有公开对刘晓波获奖这个重大事件做出任何表态，连一向比较开放的南方都市报和新京报都不准报道任何有关刘晓波获奖的新闻。迄今为止，来自中国官方的回应仍然是外交部发言人马朝旭上星期五当天的评论。马朝旭称刘晓波是罪犯，把和平奖授予他是对和平奖的亵渎。</div>

							
							
							
							
