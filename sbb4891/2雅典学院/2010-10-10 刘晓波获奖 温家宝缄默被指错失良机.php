从和温家宝的关系角度讲，体制内外批温的力量占据了很大的版块----保守派权贵集团是批温家宝的，保守派为什么批温呢，因为近十年的权贵资本主义改革使得他们掠夺了大量的民间财富，温家宝要搞民主，民主一旦搞成，人民要是来个&quot;秋后算账&quot;的话，必然影响到权贵集团的既得利益，所以他们是不遗余力的批温；第二种批温的力量是民粹主义毛左派，他们将温家宝看做是权贵资本主义改革的忠实执行者，将因这种改革而造成的大量社会罪恶都推到温家宝的头上，要求老头子来承担全部的责任。从社会批判的角度讲，毛左派们是有些道理的，他们在自己所发的《告全国人民书》中一口气喊出了十四个&quot;造反有理&quot;，客观地说，我也认为其中的八九个&quot;造反有理&quot;确实是有理的。但问题是他们将方向搞错了，他们试图借助毛泽东独裁体制来引领中国的未来，这自然和温家宝所倡导的普世价值观是&quot;牛头不对马嘴&quot;，自然，他们要批温家宝，这种批判当然是错误的；第三种批温力量来自于民间右翼的一些自由主义文人，其中以余杰为代表，他们认为温家宝光说话、没有行动，是在&quot;表演&quot;，是在&quot;作秀&quot;，并冠之以&quot;影帝&quot;的&quot;荣誉&quot;。<br />
这里我们一定要注意，对于政治家来说，他们的一个主要行为就是&quot;说话&quot;，就是通过&quot;说话&quot;来表明自己的行为取向。前不久，胡（石根）老师明确提到对于政治家来说&quot;说话就是行动&quot;，崔卫平也在最近发表的一篇文章里公开表达&quot;在政治生活中，说话便是行动。这也是民主政治的特色所在&quot;（见《政治言说》），因此，对于政治家的言论我们不要仅仅理解成一般的普通言论，他们的言论实际上是一种社会动员。话又说回来，如果温家宝说民主、倡普世价值和改革不是行动，只是作秀、表演的话，那么能不能说余杰等人倡民主和普世价值也是作秀、也是表演呢，因为这在逻辑上应该是成立的，余杰等人除了说话、发文章以外，也没有见到他有其他的行动，他没有去组党、他也没有参与街头化的&quot;围观&quot;，也没有为访民打过官司，但我并不会因为余杰只是写文章说话就认为他是在表演和作秀，相反，我认为他追求民主是很真诚的，而且他的写文章说话就是他的政治行动。问题在于我们不能&quot;只许民间放火，不许州官点灯&quot;，我们应该执行同样的逻辑和标准，否则的话，我们就会掉进自己挖下的逻辑陷阱里。<br />
在分析了这三种批温力量的错误后，我们再回到我刚才提到的&quot;政治大智慧&quot;问题上。大家想一下，在一个涉及十四亿人口的大国里，在一个涉及九百六十万平方公里土地的大国里谋求民主变革，这是一个前无古人的事业，把这样一个伟大事业的希望完全寄托在民间的力量身上，到底行不行？我认为这是不行的，当年共产党搞革命的时候，还要搞&quot;统一战线&quot;、还要开辟&quot;第二战线&quot;呢，并把&quot;统一战线&quot;看做是自己&quot;革命成功&quot;的&quot;三大法宝&quot;之一，俗话说&quot;洋为中用，古为今用&quot;，共产党当年的成功经验虽然还不能说&quot;古&quot;，但对我们最起码是应该有所启示的。我们应该把各种力量都集合起来，用我的话说就是&quot;合纵连横&quot;，所谓合纵就是把民间寻求民主的各个板块集结到一块，无论是老民运、新维权、还是家庭教会、自由派等等都先&quot;合&quot;起来，&quot;合&quot;起来干什么呢？合纵抗&quot;秦&quot;，抗击党国权贵集团对改革的阻挠。<br />
但是光靠民间的&quot;合纵&quot;还是不行的，我们还要执行&quot;连横&quot;的政策，就是把执政党内部愿意进行改革的健康力量和进步力量也拉到中国民主转型的轨道上来。大家想一想，在一个拥有将近8000万党员的队伍里，如果没有一批愿意为这个国家承担责任、愿意真正为这个国家谋求美好未来的人的存在，那是不可思议的事情。而从历史事实上看，共产党建政以来，确实是存在着这么一批健康力量的，否则无法解释像彭德怀、胡耀邦、赵紫阳的表现，他们在台上的表现是赢得后人的尊敬的，我们总不能说他们几个人在台上的时候也是作秀吧。<br />
回到温家宝这里来，纵向考察49年以来的历史，我认为在近六十年的共产党高层官僚系统里，温家宝要比所有其他的人要走得远、包括比胡耀邦和赵紫阳都要走得远、走得更符合我们的方向，所以我们要对以温家宝为代表的党国体制内的健康力量加以团结，要帮、要扶、要拉、要推！<br />
<br />
<br />
赵常青：好，第一论各抒己见阶段结束，现在进入自由讨论阶段，由于时间关系，每人发言控制在3分钟之内。<br />
<br />
胡石根：我在这里略作补充，我们必须明白&quot;挺温&quot;就是挺我们自己，如果我们不明白这个道理，就要犯政治幼稚病的错误，就要犯巨大的战略性错误。我们和余杰一样也认为温家宝现在只是画了一个饼，但是我们现在就要一个真的饼，我们必须抓住温家宝讲话的机会来弄假成真，利用温家宝的讲话把我们发展起来。大家必须明白，搞政治跟做买卖一样，做买卖对市场必须敏感，否则是做不成功的。搞政治也必须具有政治敏感性，我们现在要做的这个民主转型事业就是一个很具现实性的事业，现实的政治需要我们抓住一切可能抓住的政治机会，&quot;挺温&quot;就是我们要抓住的一个现实的具有战略意义的机会，挺温就是挺我们自己，就是挺中国的民主事业！<br />
<br />
陈永苗：实际上我也不是要根本否定温家宝，而是说怎样才能恰到好处的利用温家宝。我们必须明白一个道理，凡是共产党所赞同支持的，都是有问题的，如果我们对他所倡导的东西的复杂性没有弄清就盲目挺进，就会被他们所利用。温家宝的讲话不能理解成是他个人的讲话，他是代表中共在发言，要将他放在共产党六十年的大框架里考察，他们老早就在喊&quot;改革&quot;，天天在喊，喊了十年、百年、一万年，他就不动，这种喊叫还有什么意义呢？我们这一代年轻人大部分是行动派，强调行动的力量，我们一定要用行动的标准来评判温家宝，而不是仅仅看他说了什么。同时，我们也要给老百姓传递这样一种信息，那就是，在中共改革派之外，民间也有一种新诞生的可以领导中国民主转型的力量的存在，让老百姓在我们当中看到希望。另外关于党内民主派的问题，我们衡量的标准还是看他的行动，而不是说话。<br />
<br />
范亚峰：在政治上我们不要犯幼稚的毛病，前若干年，党国将民间的空间压缩的很小很死，民间就没有说话的空间，现在民间借助温家宝的讲话有了一些说话的空间，这是好事，但我的判断和常青不一样，常青认为温家宝是真诚寻求民主变革的，但我看来，在判断民主转型的前景问题上我们也要抛弃&quot;悲观派&quot;或者&quot;乐观派&quot;这种情绪化的语言，使用政治理性进行冷静的分析，温家宝的言论说到底就是一个利益上的考量，没有别的，我们要推进中国的民主转型事业，就必须认识到这一点。<br />
<br />
张博树：刚才亚峰提的几个建议很好，胡（石根）老师提到挺温就是挺我们自己，在座的大概不会对此有什么意见，常青谈到&quot;合纵连横&quot;与&quot;民间政治大智慧&quot;几个概念都很好。我们现在能不能促成温家宝做点什么具体的事情，或者说我们来做一个局，给温家宝开出一张试卷，让他答题，卷子一定要公开，最后的署名应该是&quot;合纵连横&quot;的局面，这样看他如何反应，如果能做，就证明他不是在作秀，如果能做的不做，那这顶&quot;影帝&quot;的帽子就戴定了。<br />
<br />
胡石根：我说一句话，温家宝说自己时间不多了，剩下的时间将努力从事政改，好，我们现在就一起来做这件事情，我们帮你设计政改方案，我们也帮你造舆论，但温家宝必须自己争气，必须在剩下不多的时间里有一些实质性的动作，比如说我们民间将政改方案提交给他，然后，他交给政治局讨论，讨论通过，就执行政改方案，&quot;党中央&quot;不批准，好，辞职，在这种情况下，你既就是什么事也没有做，也可以万民仰望、流芳青史了。<br />
<br />
范亚峰：我们可以在我们和温家宝之间寻找最大公约数，我们可以将一些具体的政改建议如废除劳教制度、司法独立制度、言论自由、结社自由等等弄出一个新的政改纲领，然后集结各种力量参与其中，其社会效益、政治效益应该是非常好的。<br />
<br />
许志永：我觉得永苗的思路是错误的，对于温家宝而言，你不管说他表演也好，作秀也好，但在常委九个人里面就他一个人站出来说了话，你现在把说了话的温家宝架在火上烧烤，这显然是不妥的，你应该把批判的目标瞄准其他几个人，这样才具有更大的合理性。<br />
<br />
永苗：瞄准不了，我们的子弹是射不出去的。<br />
<br />
亚峰：这里面涉及一个制造局面的问题，在这九个人里面就只有温是变数，我们就要想办法把这个变数推到极致。1989年学生绝食从政治逻辑上讲是正确的，学生把八十年代的学潮一步步的推向极端，最后以生命来抗争，迫使邓小平来做决断，你是要千古流芳还是要死保权力。现在虽然情况不同，但对于温来说，我们仍然要将他不断的向民主化的道路上推，你不是要搞政改吗，好，那就来真的，如果来真的受到党国阻挠，好，辞职，如果不辞职，那就是表演，通过这种推向极致的方式鞭策温家宝上轨道，这应该是可取的选择。<br />
<br />
赵常青：需要注意的是，我们不仅要帮推温家宝，就是对于胡锦涛，我们也要尽可能的将他往我们这个方向拉扯。我曾经在一篇公开发表的文章里说胡锦涛&quot;只是一个做大官的命，而不是做大事的命&quot;，从他进入共产党官场以来，事实情况就是这样，基本上是四平八稳，做官、做大官、做最大的官，一个地地道道的&quot;官迷&quot;----喜欢当官我们不反对，只要你不反对政治变革，我们都能容忍，但问题在于他是当今中国&quot;最大的官&quot;，我们对此不可掉以轻心，从他和温家宝的关系上讲，只少截至目前，他还没有公开撕裂彼此之间的合作关系，他还需要温家宝来给他挡风遮雨。因此，我们应该在挺温的同时，拉一把胡，至少要防止在中国社会大转型的关键时期，胡锦涛被权贵集团绑架为&quot;镇压派&quot;，赵紫阳曾经对杜导正说过：&quot;温家宝是个好人，胡锦涛是个明白人&quot;，我们希望胡锦涛在同样为期不多的时间里做个&quot;明白人&quot;，我们必须提醒他民主变革是世界性潮流，千万不要留恋权贵集团的怀抱，那是一个大泥坑、一个大火坑，我们要拉胡走上正确的轨道，而不是放任他往泥坑里滚、往火坑里跳，那是不负责任的。总之，我希望我们能走出一条&quot;合纵连横&quot;的团结道路，在民主的旗帜下，八仙过海，戮力同心，共同促成中国的民主转型事业，共同为我们的国家开辟出一个万紫千红、花果飘香的未来！<br />
<br />
<br />
胡石根：我在这里用一个实际的例子来说明我们现在的策略。就拿福建三网民围观事件来说，当时网友们公开将温家宝说过的话&quot;公平正义比太阳还要光辉&quot;制成横幅拉上马尾街头，表面上看网友们是在挺温，但实际上网友们要达到的是另外一个目的，而且最后的效果很不错。那么现在我们的挺温就实际用意而言和当时的网友们毫无二致，我们必须明白这个道理。<br />
<br />
赵常青：现在时间已经到六点半了，研讨会结束，准备吃饭，大家在饭桌上可以继续讨论相关话题。<br />
<br />
编外花絮：在吃饭的时候，许志永提到9.18游行抗议的事情，大家都同意姚通过各种方式来推动9.18游行的批准，而且建设性地提出了一些口号，如&quot;内争人权外争主权&quot;、&quot;钓鱼岛上去钓鱼&quot;、&quot;钓鱼岛上去观光&quot;等等。<br />
