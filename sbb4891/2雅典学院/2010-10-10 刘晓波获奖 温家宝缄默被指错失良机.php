													<h2>北京召开&quot;温家宝与中国社会转型&quot;研讨会</h2>
						
						
																											<div id="postmessage_871260" class="t_msgfont">北京召开&quot;温家宝与中国社会转型&quot;研讨会<br />
周五, 2010年 09月 17日 07:53 对华援助新闻中心 - 新闻中心<br />
<br />
对华援助协会北京讯&nbsp;&nbsp;2010年8月20--21日，国务院总理温家宝在考察深圳时做了重要讲话，8月27日温家宝又在国务院召开的依法行政工作会议上做了同样重要的讲话，温的这两次讲话在海内外的舆论界引起了极大的反响。9月15号下午3点半到6点15分，一批身在北京的学者和民主维权人士在北京再次召开了一次小型研讨会，会议纪要整理如下。<br />
&nbsp; &nbsp;&quot;温家宝与中国社会转型&quot;研讨会纪要<br />
会议开始，赵常青首先向大家介绍了温家宝两次讲话的主要内容，并将2006年9月以来，温家宝在不同时空下有关民主、法治、自由、人权、人民尊严、公平正义、反腐败、权利制衡、政体改革等方面的主要言论向大家做了介绍，也就温家宝所受到的各种力量的批评向大家做了简单介绍，然后与会者开始围绕温家宝的相关言论并联系中国社会的转型问题分别发言。<br />
<br />
陈永苗：根据观察，在座的大部分好像都是挺温的，我有些不同意见，我就第一个发言了。我谈三个问题：第一，中共党内民主派只有等到中共倒台后才能出现，中共不倒台，就不会存在所谓的&quot;党内民主派&quot;问题；第二，我们究竟应该以什么样的态度来对待温家宝的讲话，是以做主人的态度呢还是相反的态度，他说得好，我们可以表扬他、可以利用他，但我们不能无原则的唯&quot;温首是瞻&quot;；第三，我们应该立定一个判断的标准，我认为对待温家宝应该以他做了什么为标准，而不是看他说了什么，这是最重要的。<br />
<br />
陈青林：<br />
我既不是&quot;挺温派&quot;，也不是&quot;批温派&quot;，硬要说我是什么&quot;派&quot;的话，我就是&quot;救火派&quot;，救谁的火？救温家宝的火。作为这个国家的总经理，很显然，温家宝近几年遇到了很多问题和麻烦，自然灾害就不说了，诸如经济危机问题、依法行政问题、吏治腐败问题、制度转型问题等等都让他很头疼，而且他现在因为倡导普世价值问题还受到了这样或者那样的批评，真是一团乱麻。所以我在这里建议大家为温家宝走出困境来支招。我个人的&quot;救火&quot;方案实际上是向大家提出三个问题：<br />
第一，如何在2010年帮助温家宝摆脱国际国内资本家财团的绑架；<br />
第二，在2011年，如何帮助温家宝在加重税负和乱发货币之外找到第三条道路，从而使中国的经济能够更加健康的发展；<br />
第三，在2012年，如何能够推动温家宝在官僚板块、资本集团板块和人民板块之间设计出一套准宪政模式来推动中国的民主转型？<br />
这些问题交给大家，我就说到这里。<br />
<br />
张辉：<br />
温的讲话在中国民间社会引起的争议还是很大的，对于温家宝的讲话究竟是信还是不信，是帮他呢还是不帮他，是淡然局外还是加以抨击，不同的人有不同的表现。就我个人而言，任何人倡导普世价值、倡导民主、自由、人权、法治，我都应该是欢迎的、鼓励的，温家宝来说这些话，我也欢迎的。<br />
<br />
我认为未来社会的转型还是要首先从意识形态的纷争开始，没有一场争论，民间的任何一种力量都不容易切入到社会转型当中去。另一方面，我也希望大家看一看温家宝到底能做哪些事，哪怕是一些很微小的事情，如废除劳教、解决访民的问题。这些问题对于温家宝而言，应该不是特别难。他说一点好听的，我们欢迎，他做一点有利于改革的事情，我们都应该报以掌声，加以推动。<br />
<br />
最后我认为，当下这个社会已经不存在特别强大的强人政治，&quot;一言兴邦&quot;的时代结束了，我们这个社会已经是个多元社会，温家宝是其中的一&quot;元&quot;，我们在座的也其中的一&quot;元&quot;，我们可以把希望寄托在温家宝这一&quot;元&quot;里或别的什么&quot;元&quot;里，但更重要的是我们还是应该把希望寄托在我们自己身上，我们自己的崛起才是真正的希望所在。我们也需要说更多的话、做更多的事情来推动我们这个社会的变革。<br />
<br />
焦国标：<br />
<br />
我想温家宝说了这么多话，他如果生活在邓小平时代，邓小平一定会说他是&quot;资产阶级自由化&quot;的代表，但现在党内没有一个人敢公开站出来这样指责他，执政集团内部现在也没有类似邓小平这样的超级政治强人来进行幕后操纵，胡锦涛本人也不会站出来做这样的恶人的，因此温家宝的系列讲话实际上是不会有太大的风险的，在政治上是安全的。从民间的角度讲，我认为现在还是应该鼓励温家宝继续向着这个方向前进。<br />
另外一个方面， 我想谈谈我对薄熙来的看法。从政见上看，薄熙来完全是反向的做法，唱红歌，怀念毛，政见完全与我们向左，但我觉得让他继续向着&quot;左&quot;的方向走下去，也未尝不可。我认为薄熙来想完全脱离法律框架的任意胡来可能性很小，但他在一个强调大一统中央集权的国家里想另辟一个&quot;左派&quot;政治特区也未必不是好事。想当年，毛泽东他们要搞共产主义，胡适当时就向蒋介石提议让毛共党到东北区做共产主义试验，当然老蒋没批。现在薄熙来想在重庆搞一个&quot;红色&quot;特区，以显示自己与中央的某些区别，我认为是好事，我认为只要不是超越法律框架，就应该给各种政见以平等的机会和起码的尊重。我个人不会特别的倾向于哪一种政见或哪一种观点，但我认为，社会生态、政见生态越多元，对于我们的变革越有利，对于温家宝本人来说也会形成一种保护。<br />
<br />
<br />
胡石根：<br />
<br />
温家宝的讲话在民间引起了巨大的讨论，这是若干年来很少有的事情，民间的讨论超过中共党内对温家宝讲话的学习和讨论，这本身也表明民间政治理性和政治智慧的成熟，我们看到近期光北京就召开了四、五次这样的研讨活动，外地如贵州、浙江、广州等地也展开类似研讨活动，在美国，胡平他们在纽约也搞了类似的研讨活动，这些活动都带有挺温的性质。当然国内的一些网站如乌有之乡等批温的调子很高，在推推上，也有好多批温的，总的来说，从民间的角度观察，从过去的批温一边倒发展到现在批温和挺温的处于一种相对均衡的状态，这是很为可喜的事。这既表明民间政治生态已经有着比较丰富的内涵了，也表明民间政治智慧经过三十多年的曲折探索和积累而渐趋成熟。尤其是我们现在讨论温家宝这个讲话要注意这样一个问题，温家宝之所以在现在这个时刻发表这样的讲话，民间之所以在现在这样的时刻进行这样热烈的讨论，这本身说明中国社会的发展现在已经到了一个非常关键的转折点上了，这也是我们今天要把讨论温家宝讲话与中国社会转型联系到一起的重要原因。<br />
我本人虽然也不是明显的挺温派，但我对于温家宝的亲民表现、对他的讲话我是持赞同态度的，这种赞同并不意味着我们要去做他的附庸，相反我们主动搞的这种研讨表现了我们的独立意识和主体意识，没有哪个街道办通知我们这样做。我们之所以要搞这样的研讨会有一个重要目的就是要壮大我们民间的力量，实现民间各种力量的交流与汇合，通过这种交流汇合来寻求我们的共识，来协调我们的行动。同时也通过这种研讨交流来改变过去的那种官强民弱、极不对称的朝野政治生态，通过这种讨论交流促进朝野间的良性互动，并最终推动中国社会的和平转型----从现在的专制政治转向民主政治，这是毫无疑问的、是必然的，我们一定要有这个信心！<br />
<br />
在如何对待挺温和批温的问题上，我认为两派都有其价值，批温是为了防止温家宝向错误的方向发展，挺温呢，则是对温家宝已经做得比较好的事情的一种肯定和鼓励，希望他继续向着正确的方向走下去，如果他走错了路，我们还得起来批评他。<br />
最近秋风发表了一篇文章，文章的总体思路是要求民间结成健康的力量联盟，来共同打败毛左派和权贵集团，文章当然是很有价值的。但在我这里，我觉得还应该再做一下仔细分析。毛左派也好，权贵集团也好，第一，他们都不是铁板一块；第二，他们的思想也不是完全一致；第三，对于毛左派的主张和要求，我们也要去借鉴他们的某些智慧，如毛左派对现实的严重不满和批判、对社会变革的诉求，对社会弱势群体的关心，是有其中的合理性的，这与我们在某些方面的主张与诉求是一样的，是不矛盾的，我们不应该一杆子打倒一大片，要反对&quot;洪洞县里没好人&quot;的做法。就是对太子党、对权贵集团也不要一下子全部砍死，秦晓不就是太子党吗，不就是从权贵集团中出来的吗？既就是对于共产党我们也要抱着这样一种态度，我们不对这个党抱希望，但我坚信在这个有着7000万党员的队伍里一定有要求变革、要求进步、要求民主转型的力量，我们一定不要忘记民间力量与他们的结盟，这对于中国社会的未来转型具有战略性的意义。<br />
<br />
<br />
张博树：<br />
<br />
我先对刚才永苗的观点做一个简单的回应，你这三条我有两条半都是不同意的，第一，你认为党内民主派只有等到共产党垮台的时候才会出现，这是我不赞同的，如果真要等到共产党垮台才会出现党内民主派的话，也就不存在什么党内民主派了，那已经是另外一个概念了，我们说党内民主派是有意义的，恰恰是从党内民主派在社会转型过程中所能起的作用这个角度出发的，否则是没有什么意义的。我认为像胡耀邦、赵紫阳、朱厚泽等人都可以说成是党内民主派或党内开明派，而且这样的党内民主派远不止一两个人。第二，永苗谈到温家宝光说不练是不行的，不仅要看他说什么，而且要看他主要做了什么。我认为这样的批评有些简单化了。中共党内的说和做其实是很微妙的，大家如果仔细去研究赵紫阳的回忆录和李鹏日记的话，就会发现在八十年代的党内斗争是很激烈的，从话语修辞等细节方面都会推敲出这样的结论。在最近一期的《炎黄春秋》里登载了我的一篇有关朱厚泽的文章，朱厚泽就回忆说，在八十年代他当中宣部长的时候，想以纲领性的文章或开会的形式来全面推动政治改革那是很困难的，甚至是根本做不到的，只有采取化整为零、撒花椒面的方法今天在这里说两句、明天在那里说两句，以求社会舆论的逐渐转变，这算不算是&quot;做&quot;呢，我看应该是算的，不能说那是'光说不练'，同样，具体到今天温家宝的头上来，他在这样一个后极权社会要做一点事也是很困难的，他也要灵活多变，以这样或者那样的方式来努力促成气候的转变，因此不能简单的说温家宝光说不练。第三，永苗刚才谈到我们究竟应该是用一种奴隶的态度来对待温还是以一种主人的态度来对待温，我想这不是一个问题，我们要建立的是一个公民社会，我们对于总理也好或者一个普通公务员也好提出一些意见和建议，提出我们的一些肯定和批评是正常的，我想，不能认为我们批评了他就是以主人的态度看待他，我们表扬了他就是以奴隶<br />
的心态去仰望他，这样的逻辑是不成立的，我认为我们既不要用主人的心态也不要用奴隶的心态，而是以平等的心态来对待他，这就行了。<br />
然后我要从正面谈一个问题，那就是对于温家宝在最近几年的讲话和行动到底应该怎样看的问题，我基本上赞成刚才胡（胡石根）老师的观点，如果我们要从中国社会的政治转型这个高度来考虑这个问题的话，最基本的逻辑链条是很清晰的，什么链条呢？第一，中国未来的转型以和平转型最好，和平转型意味着因社会动荡而付出的代价相对要小，转型成本低，我想没有多少人希望社会转型的时候出现大动荡；第二，既然大部分人渴求社会和平转型，就必然要求朝野之间能够实现良性互动，至少民间是这样想的。问题在于第三，这种朝野见的良性互动是怎样可能的？我认为要解决这种可能性，首先，民间必须发展壮大，如果咱们自己很弱势，那就什么也不要提；而要壮大我们自己就必须促使对方阵营的分化，因为对方阵营的分化是有利于民间的成长和壮大的，只有对方分化了，民间才能壮大起来，民间壮大了，朝野见的良性互动才会逐渐转化为现实，如果对方阵营没有这种分化，则互动、良性互动都是提不上日程的。<br />
