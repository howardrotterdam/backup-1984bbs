																											<div id="postmessage_4375" class="t_msgfont"><a href="http://politics.people.com.cn/GB/113795/8369501.html" target="_blank">http://politics.people.com.cn/GB/113795/8369501.html</a><br />
<br />
网友：一点灵光 <br />
2008年11月19日14:59&nbsp;&nbsp;来源：人民网强国博客<br />
<br />
从胡主席主席同卡斯特罗握手的这张照片发现，胡主席竟然没戴眼镜，平时看习惯了他戴眼镜的儒雅照片，这张照片真是不通寻常。这不免让我猜想其中的深刻意味。我想肯定不是主席忘记了带眼镜，主席一贯严谨细致的作风绝对不会出现这种情况，思来想去，还应该是想起胡主席的细心。<br />
<br />
　　胡主席在国内考察工作时的感人细节恐怕举不胜举。<br />
<br />
　　最让人难忘的是主席在冰冷的冬天亲自品尝村民院子里的自来水。1月11日至14日，胡总书记到安徽省考察工作。看望在淮河发大水后恢复重建的阜阳村民时，亲自打开村民院里的水龙头喝了一口冰凉的自来水，这一个细节在新闻联播播出后很多人被感动了，有网友留言说，“阜阳已进入农历冬季的三九寒天，气温已降至零下好几度。而我们的总书记，不但要看农民院子里的水龙头里是否有水，还要亲自尝一尝冰凉的生水，试试水质如何。对百姓生活细节如此关心，怎能不令人感动？”<br />
<br />
　　村民家里亲自看一看百姓的存折上补助金是否如实发到，行路中停下车亲自拨开小麦上的雪看看今年的小麦生长情况，田间地头和相亲边一起劳作，边问亩产收入情况，具体到几块钱....... 这些感人的细节都体现了主席以人为本的执政理念，体现了主席热爱百姓，心系百姓的宽大胸怀，怎能不让人感动？<br />
<br />
　　从这些细节中我们不能不难明白主席为什么没戴眼镜。<br />
<br />
　　首先，我想是卡斯特罗年事已高，身体又刚刚康复，<strong>主席戴眼镜的话，在这些相机的闪光灯下，担心会因为反光影响老卡的心情</strong>，这种细致也看出胡主席对老卡关心是多么的无微不至。<br />
<br />
　　其次，我想我们不带眼镜的时候一般是在家里，是一种放松的心态，老卡是我们的老朋友了，主席不戴眼镜更让老卡感到自己的老朋友来了，老卡的心里肯定也是暖和和的。<br />
<br />
　　胡主席的能如此细致周到，不仅老卡是幸福的，我们的心里是幸福的，这使我想起了他的一句话，“情为民所系”，有这样的人做我的主席，为我们老百姓着想到每一个细节，老百姓能不高兴吗？能不幸福吗？</div>

							
							
							
							
