													<h2>如果薄熙来发动一场文化大革命……</h2>
						
						
																											<div id="postmessage_439447" class="t_msgfont">今天看到一条新闻，报道重庆市委书记薄熙来与当地300名学生亲切交流。不知道是因为我最近正在看有关文革的书，还是我自己写小说的大脑又出了状况，看着看着竟然发生了时空错乱，山城重庆变成了当年的天安门，而薄熙来却披上了毛泽东的光辉形象……<br />
<br />
 <br />
<br />
新闻报道是这样开始的：雨后初晴……市委书记薄熙来坐在同学们中间……与27所高校的300名学生进行了亲切的交流……<br />
<br />
 <br />
<br />
这个时候你还不一定能够想到当时站在天安门城楼亲切接见学生的毛主席，不妨继续看下去：<br />
<br />
薄熙来问大家：“打黑除恶你们赞成不赞成？”全场300多位同学声音响亮有力：赞成！在一片赞成的声音中，薄熙来阐述了打黑的理念，并指出是得到了人民群众支持的，薄书记还以告诫的口气警告了“党和政府”：党和政府必须头脑清醒，坚定不移地把打黑除恶进行到底。――这里的“党和政府”显然不只是指重庆的“党和政府”，因为重庆的“党和政府”薄熙来的头脑是清醒的，没有必要用“必须”两字。<br />
<br />
 <br />
<br />
接下来，薄熙来又问大家：“唱红歌，读经典，大家喜不喜欢？”全场齐声响应：喜欢！在一片喜欢声中，薄熙来回击了那些攻击他提倡唱红歌讲革命传统的人，他说，（那些人）对革命文化嗤之以鼻。我们搞“唱读讲传”，他们却在旁边评头品足，不屑一顾，有的还说我们在“搞运动”，是“左的一套”……<br />
<br />
 <br />
<br />
最后会谈结束的时候，薄熙来与同学们合影留念，大家紧紧围着薄书记……<br />
<br />
 <br />
<br />
大家看到我引用这篇新闻报道，一定会认为我要对薄熙来“评头品足”一番，但我今天不说打黑，也不说唱红歌，我想说说文化大革命，因为，这篇新闻仿佛让我看到了45年前毛泽东发动文革的那一幕幕场景……<br />
<br />
 <br />
<br />
如何评价文革好像已经铁板钉钉了（参阅《下一场“文化大革命”离我们有多远？》），只是每一个人的钉子和板子都钉在不同的地方，唯一相同的是都容不得你多说一句话。以致我写的一篇文章（《十年文革与十年互联网：我们向何处去？》）本来并不是把文革与互联网相提并论，而只是想比较这两个时代里青年思考者的异同，结果竟然招致一片讨伐声，可见，不但文革不能提，就连当初生活于其中的那些被枪毙了的思考者也是不能提的，几个亿的中国人和文革一起被盖棺论定，被否定了，成了禁忌。<br />
<br />
 <br />
<br />
难道不正是这种一刀切的否定文革却又不许探讨的方式才造成了现在并不了解文革的年轻人对文革如此向往？以致让我在这篇新闻报道中看到了当时红卫兵般的热爱和激情？他们向往的是什么？向往的是某一个领袖一挥手，结果在几个月之间，全国各地几千个基层党组织被群众砸烂，当权者被批斗，有产者或者曾经有产者（地主富农资本家等）被打翻在地再踏上一只脚……这也是后来否定文革时唯一被反复提起的理由，也是文革被定为“动乱” 的最大原因。<br />
<br />
 <br />
<br />
这个问题要讨论下去会没完没了，正如我已经懒得对人家薄熙来的言行说三道四一样，我想薄熙来的重庆应该给了我们另外一个视角，该换换脑筋思考一下。薄熙来打黑唱红歌确实得到了相当大一部分基层群众的支持，这应该是“铁板钉钉”的，精英们否定的时候千万别犯了同样的错误：把普通民众们给代表了，或者认为普通民众“都不明真相”。<br />
<br />
 <br />
<br />
就我对基层老百姓的了解，只要是打黑――打掉那些和权贵有关系的黑社会，用唱红歌来对付贪污腐败，给他们一点精神支持，“净化重庆的天空”，还真不缺乏跟随者，至少比在台上的那些权贵整天折腾的东西要吸引人得多。也许有人说，薄熙来这样做不是真心的，是为了竞争上岗――这难道不是另外一批人认为毛泽东当时发动文革的另外一个原因：对付他不喜欢的刘少奇等？<br />
<br />
 <br />
<br />
历史竟然在重庆那个地方给我们开了一个大玩笑，只是这玩笑真让人笑不起来。我想提醒大家，如果薄熙来得到了当年毛泽东的那些职位，然后他振臂一呼，要把打黑扩大到全国，发动新的一场文化大革命――或者我们换一个名字“打黑唱红歌为人民服务运动”，请问，跟随他的人数会比45年前跟随毛泽东的人少吗？群众冲进衙门把那些贪污腐败分子打翻在地的速度会比45年前的中国人慢一拍吗？<br />
<br />
 <br />
<br />
当年共产党才执政17年，大多官员家里没有过多的财产，而且基本上还是一夫一妻制，现在打起来绝对过瘾多了，无数的金银财宝，高楼大厦，大家都可以分，从此不需要蜗居了，而且，还有那么多如花似玉的二奶到N奶，就算弄不上，也可以拿出来批斗一番，把乒乓球挂在她们的脖子上，羞辱一番……<br />
<br />
 <br />
<br />
批评文革很容易，因为那是一场民族以致人类的灾难，但要真正直面文革，却是至今无法在大陆做到的；讥讽一下薄熙来也不难，毕竟他站在了历史错误的一边，而且他还没有爬到随便可以下令删除我们帖子和改写历史的高位，但要正面回答薄熙来带出来的一连串问题，我至今没有看到他的同侪中有谁跳出来提出更好的答案。<br />
<br />
 <br />
<br />
薄熙来要打的黑也许不是中国面临的主要问题，中国面临的主要问题反而是为什么他一打黑就吸引了那么多民众的眼球。如果中国不是只管白猫黑猫，抓住老鼠就是好猫，把权贵资本主义发展得如火如荼，却枉顾民生与人权，迟迟不推进政治体制改革，有法制却没有法治，有宪法却没有宪政，请问薄熙来有黑可打？还有人愿意和他一起唱红歌吗？<br />
<br />
 <br />
<br />
那些掌权者是时候想一下，造成今天这种格局的原因何在了。如果说，1949年后毛泽东一直掌权，所以，他发动文革反对自己创造和维护的体制是瞎折腾，那么，薄熙来从来没有掌握过大权，现在的一切并不是他造成的，相反，现在的这一起反而造就了他。<br />
<br />
 <br />
<br />
我觉得这篇新闻的标题――也是引用薄熙来的一句话说得很有意思：有人埋怨我们打黑，自己却不干事――<br />
<br />
 <br />
<br />
这句话当然也不是冲我们说的，薄熙来不是不知道，在中国能“干事”的除非他薄熙来这种级别或者更大的当权者，至于我们这些杨恒均之流的网友就算想干事，也没有地方干啊，好不容易写一篇博文，不被删除已经是人家手下留情了。所以很显然，薄熙来这话不是对我们这些普通老百姓说的，那他是对谁说的？<br />
