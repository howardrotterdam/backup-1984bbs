胡锦涛－－真正的庸才，还是深不可测的厉害角色？<br />
<br />
胡锦涛的仕途充满谜团：出生籍贯之谜、政治发迹之谜、中选王储之谜、及他的真实面目 之谜。胡能安坐储君位达十年之久，如果不是真正的庸才，就是深不可测的厉害角色。 <br />
&nbsp; &nbsp;今年二月小布殊访华，美国国务院给布殊一行提供的中共领导人资料，江泽民、朱F基都很详尽，但胡锦涛却只有寥寥数行，因为美国政府对他所知实在不多。四月底美国政府邀请胡锦涛访美，给予最高规格接待，除了「烧冷灶」，对中国的王储作一点政治投资外，就是想借胡上门作客的机会搞清楚这位一直躲藏在政治暗影中的未来中国领导人是甚么货色，美国传媒也大捉“Who' s Hu ”（胡是谁）的谜藏，说胡是谜一样的人物。 　　<br />
&nbsp; &nbsp;其实何止美国对他们这位将要打交道对手不明究竟，连国人也是雾里看花，因此随着胡锦涛继承大统的日子一天天逼近，解读胡锦涛也就成了台湾传媒所说的「胡学热」。 　　<br />
&nbsp; &nbsp;中国的政治是黑箱政治，政治人物透明度极低。就胡锦涛一生来说，他至少给研究者提供了四大谜题，即他的出生籍贯之谜、政治发迹之谜，中选王诸之谜，以及他真实的政治倾向之谜。 　　<br />
&nbsp; &nbsp;先说胡的出生籍贯之谜。胡锦涛出生于何地，官方资料竟无任何透露。在中共官方公布的正式简历中仅说「胡锦涛，男，汉族，一九四二年十二月生，安徽绩溪人。」因此台湾名政论家有 据此误会胡锦涛是出生于安徽绩溪的农家子弟。 <br />
&nbsp; &nbsp;胡锦涛避做江苏泰州人 　　<br />
&nbsp; &nbsp;现海外见诸文字的记载或说胡生于上海，或说生于江苏泰州，但从无生于安徽绩溪之说。实际上，胡锦涛家族与安徽绩溪早无渊源，他的直系先祖最后一个与绩溪有关的是一百多年前的太祖父胡勇源，胡勇源成年离开绩溪到江苏泰州经商后，胡氏家族从此定居泰州，到胡锦涛时已历曾祖、祖父、父亲和他这一辈共四代。胡锦涛在泰州长大，在泰州念小学中学，直到考上北京的清华大学。而胡锦涛的祖父母和父母最后都老死泰州，胡两个妹妹至今仍在泰州附近的泰县生活。因此即使按祖籍算法，胡锦涛也应是江苏泰州人，而非安徽绩溪人。但胡锦涛为甚么填籍贯，非要舍近求远，拉扯到一百多年前的祖居地呢？ 　　<br />
&nbsp; &nbsp;这大概与传统的中国社会文化心理有关。中国自古以来在血缘关系上喜欢攀龙附凤，共产党人也不能免俗。朱F基刚任总理时，有人为他写家史，就是从六百年前的明朝开国皇帝朱元璋算起。因此胡锦涛要攀一百多年的真祖宗也就不算离谱了。 　　<br />
&nbsp; &nbsp;胡锦涛以做安徽绩溪人为荣，因为第一，安徽绩溪古属徽州，为中国大名鼎鼎的人文之乡，出了许多大儒（近代如胡适，但胡适与胡锦涛同姓不同族），和有钱的徽商（如胡雪岩），胡的太祖到江苏经商后，这一支族也就成了徽商，因此胡锦涛不是农家子弟；第二，在绩溪的胡锦涛这一族人是显赫的名门望族。据报导胡氏宗祠的记载可追溯的第一始祖是作过东晋官僚散骑常侍的胡炎镇，其族最显赫的族人是明朝时作过太子太保、兵部尚书的抗倭名将胡宗宪。有这样的祖先，胡锦涛岂不与有荣焉？ 　　<br />
&nbsp; &nbsp;至于胡锦涛是否耻于做江苏泰州人？江苏泰州位于长江以北，泰州人属于上海人称的江北人。在十里洋场的上海对江北人是非常歧视的，胡锦涛的父亲五十多年前在上海做生意，经营茶叶，不论胡锦涛是否出生于上海和在上海生活过，他对「江北人」处境的这种敏感性，应该知悉。 　　<br />
&nbsp; &nbsp;另外需要指出的是，如果胡锦涛选江苏作藉贯，众州与江泽民的故乡扬州距离不到一百公里，胡锦涛就是当今中共领导人与江地缘最近的小乡。 <br />
&nbsp; &nbsp;胡锦涛因宋平夫妇而平步青云 　　<br />
&nbsp; &nbsp;现谈胡锦涛的发迹之谜。胡锦涛一九五九年入清华大学水利工程系河川枢纽电站专业，一九六八年文革期间分配到甘肃刘家峡水电站工作，最初当技术员，后调甘肃省建委当秘书，一九七五年成为省计委一名副处级干部。据说胡锦涛那时唯一的愿望是可以离开边陲之地的甘肃调回首都北京，想来也是颇失落的。但进入八十年代后，胡锦涛却突然时来运转，官符如火，一九八○年升任甘肃省建委副主任，一九八一年进中央党校培训，一九八二年参加中共十二大，当选中央候补委员，也是该届最年轻委员、年仅四十岁，然后回到甘肃任省团委书记，一九八三年进京任团中央书记。 　　<br />
&nbsp; &nbsp;胡锦涛在八十年代初从一个微不足道的基层干部突然成为中共锐力培养的政坛新秀，这个命运的转变，普遍的说法是他获得了当时甘肃省委书记宋平的赏识和大力提携。但一个高高在上的封疆大吏怎么会留意到一个小小的年轻干部？ 　　<br />
&nbsp; &nbsp;奥妙在胡锦涛清华大学的经历。胡锦涛在清华是个很活跃的学生干部。他一九六四年加入共产党，一九六五年毕业留校作政治辅导员。宋平的妻子陈舜瑶在那段时间恰好是清华大学党委副书记，以胡锦涛学生党员干部和政治辅导员的身份，两人在清华大学应该有过不少工作上的接触。 　<br />
&nbsp; &nbsp;文革动荡年月过去后，在远离北京的西北重逢，陈舜瑶见到胡锦涛自然会有一份亲切感。胡因是政治辅导员，文革初先受冲击，后成逍遥派，没有参加过造反，对于受过冲击的前走资派来说，胡锦涛这样的大学生党员更显得忠诚可靠。而且宋平也是清华毕业的。可以想象凭着这层深厚渊源，胡锦涛自然会受到宋平夫妇的器重。 　　<br />
&nbsp; &nbsp;在中共的官场中一个人的官运是否亨通，要看你是否跟对了人。像胡锦涛的一步登天没有宋平这样的强大政治奥援是不可想象的。 　　<br />
&nbsp; &nbsp;宋平是八十年代中共实力派人物，是超级元老陈云的代言人，一九八九年后更进入中共政治局常委掌管人事大权，对人事任免一言九鼎。甚至在他十四大退下来到今天，宋平对中共政坛仍有影响力，总书记江泽民对他仍相当尊重。可以想见，胡锦涛的步步上升，在组织上，宋平有决定性的影响。<br />
&nbsp; &nbsp;胡锦涛被立为王储之谜 　　<br />
&nbsp; &nbsp;一九九二年中共十四大时，胡锦涛爆冷成为七人政治局常委会中最年轻成员，震惊中外。之前唯有该年九月号的《开放》最早报导胡锦涛成为脱颖而出的黑马。按邓小平的话来说，胡锦涛是作为「中国跨世纪接班人」的角色进入中共权力核心，即胡锦涛是邓小平指定继江泽民后将承续大统的储君。 　　<br />
&nbsp; &nbsp;据悉，在中共第三梯队众多政治新秀中，胡锦涛能脱颖而出与拉萨戒严有关，中共老人认为他忠实执行了中共对西藏的铁腕政策，拉萨戒严有功，政治忠诚经受住了考验。 　　<br />
&nbsp; &nbsp;拉萨戒严可以说是胡锦涛从政以来的最大污点。一九八八年底胡锦涛正式受命进藏任西藏自治区党委书记时，西藏局势很紧张，拉萨不断爆发藏人示威要求独立中共开枪镇压的流血冲突，前任自治区党委书记伍精华不堪重荷以患了高原病为理由留在北京不归，胡锦涛进藏是临危受命。 　　<br />
&nbsp; &nbsp;八九年一月二十八日被中共派回西藏安抚藏人的十世班禅喇嘛在他的驻锡地日喀则病逝，因为死的很突然，西藏人心震动，传闻四起（甚至有说班禅喇嘛是被陪同他返日喀则的胡锦涛下令毒死的），三月五日拉萨爆发了自一九五七年以来最大规模流血冲突，骚乱持续了两日，三月八日国务院宣布了拉萨戒严，胡锦涛忠实执行中央铁腕镇压藏人的政策，头戴钢盔，与戒严部队出现在拉萨街头。 　　<br />
&nbsp; &nbsp;虽然，胡锦涛在西藏只是执行中央的政策，但毕竟他的红顶子染上了藏人的鲜血。 <br />
&nbsp; &nbsp;从来不说自己的话却广结善缘 　　<br />
&nbsp; &nbsp;最后一个谜是所有人最关注的，这位中国未来新皇帝的政治性格究竟怎样？ 　　<br />
&nbsp; &nbsp;在印度的西藏流亡政府今年举行了多次研讨会，讨论中国这位前治藏大臣的政治倾向，探讨他上台后西藏问题是否能出现转机，但西藏流亡政府的人说，讨论不出甚么结论来，因为缺乏资料。 　　<br />
&nbsp; &nbsp;胡锦涛一生，除了拉萨戒严，平淡无奇，没有甚么可以拿得出来的政绩。大陆的政治观察家认为，胡锦涛能选出来继承大统，正是因为他的平庸。在中国官僚制度的精英淘汰机制中，枪打出头鸟，有创新精神的，有个性的，有胆识的早已被淘汰，只有谨小慎微者，唯上司意志马首是瞻者，才有可能成为各方都可以接受的人物爬到最高层。 　　<br />
&nbsp; &nbsp;但另一种观察认为胡锦涛的成功靠的是他几臻化境的人际手腕，他的通情达理，性格温和，使他「没有敌人」，与他同过学的共过事的人都对他有好感，说他是「好人」，中共高层左中右各派都能接受他。 　　<br />
&nbsp; &nbsp;关于胡锦涛待人处世圆融，北京政坛中流传着不少故事。 　　<br />
&nbsp; &nbsp;八十年代胡锦涛任团中央书记时，有次团中央开大会，大会结束后各地代表争着和中央首长分别拍照，但首长们很傲慢，说正赶着开另一个小会而拒绝了。代表们碰了一鼻子灰，扫兴而去。稍后正在开会的胡锦涛说他有事要出去一下，谁知他是出去和这些代表握手拍照，照完相后他静静回到会场，会议也未受到影响，结果是皆大欢喜。 　　<br />
&nbsp; &nbsp;有位了解胡锦涛的人士说，胡有本事让每一派的人都会认为胡是「自己的人」。当年他被左派大老宋平大力举荐，后来又获开明派总书记胡耀邦欣赏而倾力培养，下令六四镇压的邓小平觉得胡忠诚可靠可以传位，现在中共党内外的开明派又一厢情愿认为胡一旦大权在握会搞民主。 　　<br />
&nbsp; &nbsp;到底真实的胡锦涛是个甚么人？北京一些观察家指出，「胡锦涛从来不讲自己的话，他让你摸不到底牌。」他公开讲的话从来是江泽民、朱F基讲过的，他当军委副主席后，军委呈来的文件，他的批示永远与江泽民保持一致，因此军队方面的人说「胡副主席很谦虚。」据说曾有人在饭局上与胡锦涛的秘书同桌，千方百计套他的秘书发表意见，结果听到的全是四平八稳的官腔，滴水不漏。 　　<br />
&nbsp; &nbsp;从十四大起，胡锦涛在中共最敏感、最有风险的王储位上安然不动地坐了整整十年，只有两种可能：一、他真是庸才好好先生，人家不怕他兴风作浪；二、他可能是罕见的高智商权谋家，韬光养晦做到无懈可击。而且隐忍十年不动声色，自制力和意志力惊人，是最厉害的角色。如果是第二种情况，胡锦涛既可能是又一个专制独裁者，又可能是人们期盼的中国的戈尔巴乔夫。 　　<br />
&nbsp; &nbsp;在历史上这种当副手的三年不鸣、一鸣惊人的例子很多。 　　<br />
&nbsp; &nbsp;五十年代中东强人纳赛尔，叱咤风云，不可一世，他的副手萨达特沉默寡言，做事竞竞业业，对纳赛尔绝对效忠。纳赛尔说他是一个没有野心的人。但纳赛尔一死，萨达特继位，展示雄才大略，完全变了一个人，先一改纳赛尔亲苏的路线，改投西方，随后发动赎罪日战争，与六日战争打得纳赛尔一败涂地的以色列百胜军打成平手，雪了六日战争之耻。继尔挟战威与以色列签署和平条约，成为首个承认以色列生存权的阿拉伯领袖，因而成功收回六日战争被占领的西奈半岛。人称萨达特智商之高，同时代世界领袖中罕有堪与其匹敌的。 　　<br />
&nbsp; &nbsp;前中共总书记赵紫阳的智囊鲍彤说，胡锦涛这个人很精明、擅于保护自己，无人知道他在最关键时刻采取了甚么立场，也不知道他在中国的政治改革中扮演何种角色。他可能是平庸的机会主义者，也可能是伺机而行的雄才</div>

							
							
							
							
