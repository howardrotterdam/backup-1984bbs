													<h2>许纪霖谈国民党如何失去知识分子的支持　</h2>
						
						
																											<div id="postmessage_767120" class="t_msgfont">许纪霖谈国民党如何失去知识分子的支持　　来源: 东方早报(上海)<br />
<br />
　　据说“文革”期间有一句耳熟能详的名言：笔杆子、枪杆子,革命就靠这二杆子。当蒋介石的威望随着抗战结束达到顶峰的时候，很难想到短短几年之后竟然沦为知识精英眼中的独夫民贼。最终，批判的武器与武器的批判一同将国民党赶出了中国大陆。在华东师范大学历史系教授许纪霖看来，国民党政府与知识精英以及知识精英所代表的民心之间的关系变化，是很值得深究的。<br />
<br />
　　黄晓峰<br />
<br />
　　蒋介石领导的国民政府，在1945年到1949年间，迅速地失去知识分子的支持，这是什么原因造成的？<br />
<br />
　　许纪霖：首先是认同的危机。我一直有一个看法，在民主社会里，精英的作用是有限的。因为重大的决策是一票一票投出来的。怎么来动员选民、影响选民，是最重要的。但在一个非民主的社会里，精英和政府的关系是最核心的东西。中国传统上是民本政治，从儒家一直到国民党，都讲民本、民生，但是民是沉默的大多数，他本身不可能发出声音。哪怕到了近代，有了公共领域，有了现代的传媒、报纸、杂志，其中能够主持言论的还是知识分子，不是一般的小民。民是要被代表的，而代表民意民心的，恰恰是掌握了话语领导权的知识分子。在中国历史上，统治者是否得民心，实际上是是否得士心。统治者应倾听士大夫的清议和民间舆论。<br />
<br />
　　从1945年到1949年，短短四年，知识精英与国民党政府的关系发生了很大的逆转。逆转的原因，有两个背景性因素，一个是外敌的消失。从1895 年甲午海战失败一直到抗战胜利，半个世纪以来中国从来没有断过亡国灭种的危险。一个接一个，始终有外敌。这个外敌不是潜伏性的，是实实在在的威胁。外敌的存在，使得知识精英哪怕对政府有诸般不满，还是对合法的中央政府有一定的认同感，除了个别烂透了的北洋政权，比如张作霖的统治。对于国民党政府，大多数中国知识分子以前是一直怀有期待的。二十世纪三十年代在《独立与评论》内部，有过一场民主与独裁的争论。像蒋廷黻、丁文江、钱端升这些老牌自由主义者，认为独裁虽然不好，但与其存在着无数个小独裁――军阀割据，不如有一个开明的大独裁，在中央形成一个开明的威权，以应付大敌当前的国难。有了外敌，就容易形成精英与政府某种适当的合作关系。抗战胜利以后，外敌基本不存在了，亡国灭种的威胁消除了。政府不可能再制造什么外敌，让知识分子和国民为了国家的自由牺牲个人的自由，认同国民党的一党专政。战后知识分子所一致认同的口号是“和平统一，民主建国”。通过和平、民主的方式，建立一个统一的民族国家。后来，国民党想通过打内战“戡乱建国”，自然很不得人心，首先让知识精英绝对无法认同。<br />
<br />
　　当外敌消失之后，其他方面的变化就很容易导致离心离德。<br />
<br />
　　许纪霖：另外一个背景性因素是到1940年代，知识精英在利益上日益与政府体制疏离，无法通过其工作获得与其身份相符合的、有尊严的报酬和收入。北洋政府忙于内斗，对知识分子是不太重视的，常常有欠薪。国民党在1927年建立国民政府以后，非常注重拉拢知识分子，尤其是留学回来的大知识分子。 1927年到1937年，既是近代中国经济的黄金年代，也是知识分子的黄金年代。只要你不与政府作对，凡是在体制里谋到一份工作，特别在国立大学，收入是非常不错的。国立大学的教授每月有三四百大洋，过的是非常奢华的生活。你想，骆驼祥子一个月七块大洋，也可以在北平温饱了。在四十年代之前，知识精英分享了政权的好处，体制内部的知识精英大都对国民政府有认同感。虽然有些知识分子有批评和反抗，但大部分知识分子并不关心政治，有利益上的考量。但建立在利益上的统治正当性是非常脆弱的，一旦利益链发生问题，政府的正当性就发生危机。果然，1940年以后的战时中国，开始出现急剧的通货膨胀。通货膨胀伤害最大的对象，就是这些拿国家薪水的公务人员。国民党的党政人员还可以搜刮，知识精英没有什么好搜刮的，实际生活水准直线下降。一旦精英沦为贫民，对政府的态度就急转直下。假如政府与民同甘苦、共患难，知识分子还可以接受，问题是国民党政府太腐败了，贫富差距严重拉大，朱门酒肉臭，路有冻死骨，令人寒心。费正清后来在回忆录里谈到，1943年是一个转折点，蒋委员长失去了精英的认同。这一说法，虽然有夸张的成分，但的确反映了自由知识分子的心理。因为当时费正清在昆明，每天与西南联大的教授们混在一起，对人心的转向有敏锐的观察。<br />
<br />
　　以闻一多为例，三十年代在北平的时候，一个人的收入可以养家里大大小小的人口，还可以雇几个保姆；1940年以后，物价飞涨，入不敷出，只能去中学兼课、刻图章补贴家用了。后来，他会拍案而起，除了政治上的义愤，切身的生活感受是更大的催化剂。知识分子的贫困化在四十年代是相当普遍的现象。1946 年胡适从驻美大使任上回来做北大校长，他有一个雄心勃勃的十年学术计划。结果在教授会上他的方案无人理睬。教授们纷纷向校长诉苦：我们现在生活都有问题，十年以后是否还活着都是个问题，还谈什么学术！胡适听了目瞪口呆，大失所望。1940年代后期，校园已不再成为校园，到处是反饥饿、反内战、反迫害的标语。学生们三天两头上街抗议游行，教授们也民不聊生，要靠美国的救援面粉来维持生命，那是多大的心灵伤害！朱自清情愿饿死，也不领美国的面粉。说实话，他不是对美国有多大的不满，而是对政府不满。朱先生如此持重之人，竟然也在清华园里与学生们扭秧歌。国民政府从1927年开始的笼络知识分子的高薪政策，到那个时候完全失败。<br />
<br />
　　当知识分子失去了对国民党政府的认同感，在清议、舆论上会有很多批评吧？<br />
<br />
　　许纪霖：国民党舆论主导权的丧失，是其失去民心的另一个表现。国民党在1924年以后改组，学习苏俄的经验，面目焕然一新，那个时候它是一个革命党，主要依靠意识形态。国民大革命兴起以后，很多北方知识分子孔雀东南飞，开始认同国民党。为什么认同？因为国民党代表了“五四”以后的新气象。1927 年以后，国民党面临着从革命党向执政党转型的问题。在这个问题的处理上，国民党是非常暧昧的，它延续了革命党的方式，不是靠法治，不是靠建立一套在宪法和法律基础上的公共文化来获得认同。国民党还是靠三民主义教育，强行在学校设立训导处，推行三民主义党义教育。自由派知识分子、学生非常不满，完全是应付性的。虽然它在主流教育体制有不可动摇的位置，但没人相信它。公共舆论的主导权，一直不在国民党那里。“五四”启蒙的核心观念，如民主、自由、科学，与作为国民党的主流意识形态一直有冲突。闻一多之所以后来拍案而起，最早是他一度很崇拜的领袖写了一本《中国之命运》，他看了以后，吓了一跳，这不是反“五四” 吗？于是闻一多这些从“五四”走来的自由知识分子无法忍受，走上了对抗国民党的路。<br />
<br />
　　这是否说明当时是知识分子掌握了话语权？<br />
<br />
　　许纪霖：对，这就是葛兰西所说的话语领导权。从这点来说，国民党一直不占上风，处于被动挨批的位置。而共产党则非常会抓舆论。《八一宣言》以后，主张抗日。四十年代以后争取民主、自由，延安《解放日报》接连发社论，批评国民党一党专政，汇合了二战期间国际上主流的民主声音。四十年代的话语领导权相当部分还在自由派手中，像《大公报》、《文汇报》、《新民报》、《观察》杂志，力量很大。蒋介石起床之后第一件事，不看《中央日报》，不看《解放日报》，而要看《大公报》。《中央日报》都是他的声音，不要看；《解放日报》都是骂他的，也不要看；而《大公报》代表了社会一般的舆论，他不得不顾及人心的趋向。《大公报》后来获得了美国密苏里学院新闻奖，这是亚洲第一家获得这个权威奖项的报纸。<br />
<br />
　　战后的一段时间，是近代中国舆论最开放的年代。人人可以办报、办杂志，什么声音都可以放出来，很响亮。国民党压力很大，不仅有内部的舆论，而且美国派马歇尔将军来中国，要蒋介石联合共产党成立联合政府。等到全面内战爆发之后，蒋介石对舆论的处理就非常简单，谁的声音对我不满，就一家一家关。先是关激进的，然后关温和的。储安平主办的《观察》杂志影响很大，有十万订户，百万读者，一开始不敢关，后来国民党觉得实在无法容忍，关掉。连北平的《新路》杂志都容忍不了。《新路》本来是自由派当中的温和派办的，又有宋子文的背景，正面建言远远超过批评，最后也被查禁了。关是最简单的处理方式，一时似乎讨厌的声音消失，天下太平了；但民情却在地下奔涌，怨恨在暴力中积累，一点点将温和的知识分子逼到激进。储安平在《观察》上很尖锐地指出，谁在制造共产党？是国民党制造了共产党，将温和的自由主义一个个逼到了左倾。<br />
<br />
　　国民党政府失去话语的主导权，会把越来越多的知识分子赶到左边去。<br />
<br />
　　许纪霖：是的，国民党不仅容不得老冤家共产党，而且容不得中间的自由派。中间的自由派虽然没有一兵一卒，但是他们代表了普遍的民心。在争取中间派这点上，国民党是连出错招。李敖嘲笑蒋介石是“搞独裁无胆，搞民主无量”。老蒋作为一个日本士官学校出身的军人，对民主无论是理念还是制度，缺乏最基本的了解，他相信实力就是一切。在战后他缺乏作为国家领袖应有的大视野、大胸怀和大手笔，做不出他儿子蒋经国上世纪八十年代末在台湾那样的扭转乾坤的历史大动作。<br />
<br />
　　战后国共之间的力量一度处于某种平衡，于是中间力量有了施展的空间。1946年初，旧政治协商会议开完以后，民盟的罗隆基得意地对马歇尔讲：“共产党让步大，国民党苦恼多，民盟前途好。”一时似乎也有和平的希望，但一个东北问题，燃起了全面内战。国民党最初处于绝对优势，相信军事决定一切。陈诚对老蒋拍胸脯说，只要你放手让我干，保证三个月消灭共产党！但是就像我们从电视剧《潜伏》里面看到的，国民党的整个党、政府和军队都烂掉了，百分之八十的精力不是去对付共产党，而是对付自己人。人心抓不住，只能转向靠特务统治，用暗杀、镇压、抓人的办法维持政权。战后国民党看上去像庞然大物，但内部都被掏空了，对社会的控制力严重弱化，无法深入到基层，都浮在表面。到了靠特务统治维持天下，那已经是黔驴技穷，合法性建立在暴力上。那个时候，国民党基本上气数已尽，哪怕是最温和的人，也开始向左转。到1948年，蒋介石连民盟都容忍不了，压迫他们解散，统统把他们推到与共产党合作的一条路。知识精英与国民党最后的破裂，正是蒋介石一手导演的结果。<br />
<br />
　　1949年蒋介石败退台湾，知识分子都会遇到一个跟谁走的问题吧？<br />
<br />
　　许纪霖：在1948年底到1949年，从北到南，知识分子当中议论最多的话题就是：走还是留？不少知识分子内心有挣扎。跟着一个在小岛偏安的政权，能维持多久？中国晚清以后政治变动太频繁了，知识分子看得也多了，不就是一次新的朝代更迭吗？许多人根据以往的历史经验，认为朝代更迭可以接受，毕竟是汉人自己的政权，社会与文化总不会变吧。冯友兰与国民党关系比较深，当时作了最坏的打算，留下来，即便政治自由没有了，学术自由总是有的，可以作一个单纯的教书匠。但他没有想到的是，1949年以后，新中国发生的不是传统的改朝换代，而是翻天覆地的社会大革命。<br />
<br />
　　去留问题的选择，大多不尽然是政治的选择，还有文化的认同。北大清华的教授们即使接他们的专机飞到了北平，还是不走。不是对新政权有多少认识，实在是国民党让他们看伤心了，不愿为它而陪葬。国民党太烂了，新政权纵然有百般缺点，也总比国民党好吧？另一方面，许多人对家国有依恋感，依恋母校，依恋城市，依恋早已与生命融为一体的土地、风情和文化。去留的选择，对大部分知识分子来说，是一个文化的认同。<br />
<br />
　　就这样，中国知识分子，与国民党的那段从蜜月到疏离最后到决裂的历史，就这样翻过去了，他们中的绝大多数，与新中国一起走进了1949年。<br />
<br />
　　(实习生郑诗亮对此文亦有贡献)</div>

							
							
							
							
