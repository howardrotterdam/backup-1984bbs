																											<div id="postmessage_409578" class="t_msgfont">近年来中国思想界在共产党的“胡折腾”下彻底迷失方向，大批儒教渣滓在共产党的示意下四处招摇，大肆鼓吹“复兴国学”那么，什么是国学呢，事实上，所谓’国学“，是一堆儒教渣滓为了重获自己在中国思想界的统治地位而勾结中共，专为中国的西化道路设置障碍的一种为儒教维护的专制独裁分子称王称霸打下思想基础的“伪学”，一种在思想上奴役中国人的流氓学说，太监学说。直白的说：这是某些中国人的意淫，他们不但自己淫，还强迫我们的人民，跟着他们一起“淫”。这，正是为什么我们中国的现代化之路走得如此艰难，甚至还不如曾身为“殖民地”的中国香港的最大原因。这帮愚蠢的儒教分子们，一边享受着伟大的西方文明发明的电器，自来水，西医西药，一边臭嘴一张：“反对西化，复兴国学”。他们复兴的是什么样的国学？是由儒家那帮太监文人们空徒折腾出来的“太监学”。这全都是一堆根本就不可能产生任何新的发现、新的发明和新的创造的仅存在于历史文献之中的“文化垃圾”。<br />
<br />
试想，那个稍有点脸的国家，民族，会把自己国家的学说命名为“国学”呢？如此狂妄自大的学说，到底是什么玩意自然可想而知。<br />
中国的“国学”既然真像儒教分子说的那么好？怎么不去到国际上去争取一个“学说”的“冠军”去，去拿一个国际上承认的“诺贝尔奖”去，到全世界去大力宣扬、广泛宣扬去，也让别人学一学由中国人发明的“普世”的“学说”、“思想”、“观念”，这才是真正能够表现出中国人的伟大“人类”的“人性”来啊；而仅仅标榜自己（空无一物）的“国学”，说穿了，是害怕走出国门的不敢见人之“学”，事实上也就是根本走不出国门，或即使羞羞答答地走出了国门也让人深深地瞧不起的无用之“学”。这样的“国学”能够是中国人的“光荣”吗？不，这里根本就没有任何中国人的“光荣”，而是只有纯粹中国人的耻辱。<br />
说得难听一点，中国根本就没有任何有价值的“学”，所以才从中国文化的垃圾堆里拿出一个“国学”来樽约鹤车ǎ既是“说谎”，也是“吹牛”，更是“拍马”。向谁“说谎”？向中国的老百姓“说谎”；向谁“吹牛”？向全世界、全人类“吹牛”；向谁“拍马”？向中国当政的专制统治者“拍马”。如此“说谎”、“吹牛”、“拍马”的“国学”，纯粹就是中华民族的灾难、累赘、耻辱，无穷无尽的“毒药”。</div>

							
							
							
							
