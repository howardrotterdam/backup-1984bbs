																											<div id="postmessage_409669" class="t_msgfont"><div class="quote"><h5>引用:</h5><blockquote>原帖由 <i>|立春|</i> 于 2010-1-2 13:27 发表 <a href="https://1984bbs.com/redirect.php?goto=findpost&amp;pid=409663&amp;ptid=33259" target="_blank"><img src="https://1984bbs.com/images/common/back.gif" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /></a><br />
