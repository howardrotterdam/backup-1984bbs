你的表述文字上有错误吧？日本议员是世袭制么？显然与事实不符。<br />
你是不是说，老布什与小布什都当了美国总统，那么美国总统就是世袭制？<br />
能否把你“实质上的世袭”详细定义一下？ </blockquote></div>门阀制度<br />
<br />
　尽管日本民主党“掀翻”了统治日本政坛50多年的自民党，创造了“变天”局面，但“变革来了，但政治仍属家族事务”。<br />
<br />
　　日本的“世袭政治”痕迹一直都很浓重。在过去七位日本前首相中，有六位都是前议会成员的儿子或者孙子。通常有家族背景的候选人在选举中会占据明显优势，一般都能凭借其父辈祖父辈的政治招牌赢得选票和资金支持。<br />
<br />
　　这次大选中，99个自民党议会候选人，与现任或前任的议员有亲戚关系，民主党的这个数字是22个。鸠山由纪夫的外祖父石桥正二郎是著名企业普利司通的创始人，父亲鸠山威一郎曾任外务大臣，祖父鸠山一郎是大名鼎鼎的前任首相。而麻生的身世就更不用说了，他是前任首相吉田茂的外孙，其妻是前首相铃木善幸的三女。又比如，小泉家族数代从政，小泉纯一郎的祖父小泉又次郎和父亲小泉纯也均曾出任众议员。所以，日本政坛“门阀制”导致剧烈的批评。<br />
<br />
　　日本整个社会中的“门阀管理”异常明显，这不仅表现在政治体系当中，也在大企业中表现明显。尽管后来这些大公司也都引入了职业经理人进行管理，但其创始人的后代都在企业中占据重要地位。比如，丰田汽车公司中，创始人丰田喜一郎的后代也一直在公司担任要职。松下、索尼等品牌最初都是由于创始人的意念、天赋以及才能而创立的。在日本这些公司中，大都实行 “终身雇佣制”、 “年功序列”，这一套成熟的管理体系据说是松下的创始人松下幸之助确立的。终身雇佣是为了建立员工对自己企业的认同感和安全感，日本这一套管理体系非常成功。的确，在经济危机之前，对于一个普通的日本人来说，最好的职业规划就是就读一所名牌大学，然后进入知名大公司工作，兢兢业业，直到终老。<br />
<br />
　　在日本，家的意识、家业延续、重视纵式的亲子关系是家族观念的基本内容，尽管这些观念在二战后受到了极大挑战和冲击，但是，日本依然是一个家族观念很重的儒家文化主导型社会。这是门阀管理的重要土壤，无论是企业还是政治都是如此。尽管有很多学者认为，日本正在由一个群体社会逐步变为个体社会，但是很多事实表明日本人不会轻易抛弃自己的传统，日本的和服、古老的艺妓文化、古香古色的建筑依然是这个现代国家的特色，日本明治县至今依然保存着200多年前的明治时代的建筑，日本人在1999年重开了其第一辆蒸汽火车，供普通民众乘坐和重返记忆，等等。还有很重要的一点，日本人非常欣赏的一个理念就是“改善”（Kaizen），而这个词语则清楚无误地代表了日本人喜欢内部不断改良而不是推翻过去的一切。<br />
<br />
　　大公司是特定人物创立的，它有时带有一定的偶然性，但是，企业的“门阀管理”并不是偶然的，日本人重视家族，后代对先辈充满敬畏，大企业的创始人将自己的事业不断代际传承下去，这没什么特别的。特别的是，他们的家族企业在代际传承中居然也会这么成功，一直保持着竞争优势，这是令人关注的。因为，中国俗语有云“富不过三代”，但这对日本似乎不太适用。我们要思考的是，同样是家族生意，同样是大企业，为什么日本大企业管理得这么成功，而中国的民营大企业却总是爆出企业家被判刑的消息。<br />
<br />
　　门阀管理在企业中不一定是坏的，但是在政治中它却天然的带有负面影响。因为，企业运作基本不涉及民众让渡给政府的公权力，而政府管理，则涉及到民众的利益，如果门阀当道，那么肯定是特定利益者受惠，而民众利益受损。毕竟，现代民主社会，政治不能只玩小圈子。</div>

							
							
							
							
