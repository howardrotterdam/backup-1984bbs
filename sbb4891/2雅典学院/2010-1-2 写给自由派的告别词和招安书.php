													<h2>写给自由派的告别词和招安书</h2>
						
						
																											<div id="postmessage_410376" class="t_msgfont">写给自由派的告别词和招安书<br />
<br />
一<br />
出于对自由主义相当程度的认同，也出于为儒门寻找英雄、让英雄成就圣贤的素愿，东海曾在自由派阵营混迹多年。总以为其中正人君子英雄豪杰比例高些，比较容易接受或归本儒家。责备贤者，春秋之义，东海多么希望自由大侠们百尺竿头更进一步，更好地成就自我、成就中华。<br />
<br />
更重要的是，东海深刻认识到，制度建设与文化建设、民主启蒙与道德启蒙同等重要，儒化与民主化是一而二、二合一的事。没有儒家文化和道德垫底，自由理想将遥不可及，民主追求将匮乏力量，纵然侥幸实现，也必是劣质民主，利弊难料。我在《台湾尚如此，大陆何以堪？---再回司徒一先生》中指出：<br />
<br />
“制度与科技不一样，异域移植的制度与本土文化如不能作深度勾通，必然会发生各种恶性冲突和变异。由于政治、社会、历史的种种原因，儒家没有直接开出民主制，但儒家的民主思想则颇为原始，本身接纳民主制度毫无障碍。关键在儒家主体能否挺立起来并获得政治机会。<br />
<br />
在儒家看来，台湾民主不算一个好样板。由于制度的文化背景不深、社会的道德根柢太浅，台湾民主颇为劣质。台湾尚如此，大陆何以堪？故我虽盼望和追求大陆民主化，但也一直有一大隐忧，耽心未来中国民主的质量。大陆对中华文化的摧残曾百倍于台湾，而今虽有所恢复，与台湾水平比仍有不如。<br />
<br />
文化的传承和道德的建设，应从思想先锋人物、社会中坚群体开始，从民主自由队伍开始。东海数年来苦口婆心棒喝狮吼，在开罪特权集团的同时不惜开罪大小真伪各方英杰，根本原因在此。”<br />
<br />
二<br />
自由主义是好东西，其精华理应为我们所汲取，但不足以主导的政治社会的大变革。中氲拿裰骰必须在以儒家为主统的中华文化的指导下进行，中氲拿裰鞅匦朐根于本土文化之中，必须是儒式的民主。这是东海的坚持、儒家的原则。<br />
<br />
为此，我写了大量文章深入阐述道德与制度、儒家与民主的关系，对独立中文笔会同道及其他民主人士的偏见谬论进行严正批驳，同时由浅入深逐步推出“大良知学”。东海之学有述有作，横贯纵继，纵集历代儒家之大成，横汲佛道与西学之精华，于古圣西贤，优处长处不敢昧，不足之处亦不敢讳。<br />
<br />
从“异”处说，东海儒家与古今中外任何一家一派学说包括历代儒家都有所不同，从“同”处说，则不仅与佛道诸家有相同之处，与西方自由主义尤有相通处。在外王学中，以法治为德治之基，以宪政为仁政之新，以民主为新王道政治初级阶段，对自由主义精粹汲纳无遗。<br />
<br />
<br />
我本来想，自由派之所以普遍反对传统排斥儒家，是出于传统的陌生与文化的误解，只要有人将儒家的正理精义及相关历史讲说清楚，必能让他们中的多数翻然悔悟、弃旧图新。<br />
<br />
不料东海乐观过头了，高估了民主自由派的“文化程度”。在这个阵营中，绝大多数对儒佛道文化抱有“先天性”的轻蔑，拜基督或倾向基督蔚然成风，自由主义与中华文化的关系亦未因东海的棒喝狮吼而得到有效改变，在多数人心目中它们仍然是“你死我活的关系”。<br />
<br />
儒家本具民主自由的思想原素、对西方民主制度的学习汲纳工作在以熊十力为代表的新儒家群体中早已开始，对于诸如此类常识和事实，在这里是得不到理解和尊重的。相反，儒家、孔子以及亮明儒旗之后的东海，都是嘲弄、讥讽和抹黑的对象（当然也有对东海颇为了解友好并能尊重儒家的，极少数）。基本没有正常的争鸣。讲道理，他们在新老左派及广大党用学者那里是强项，到了东海儒家面前则成了弱势群体。<br />
<br />
之所以如此，除了自由派自身的文化内存问题，更有历史原因、时代趋势、和政治文化因缘在，不是一时能够解决的。如果把马列主义比作墨家、自由主义比作杨朱之学（正巧马家的共产主义精神与墨家的利他主义、自由主义的意识形态背景个人主义与杨朱的利己主义不无相似之处），现在中国自由知识分子正处于“逃墨必归于杨”的状况，“逃杨必归于儒”的时期还没有开始，那要等到西式民主的弊端和儒家文化的优势都被世界普遍认识之后。<br />
<br />
三<br />
浩气真言惹人厌，深悲大憾复谁知。东海肩头，政治责任重（这里的政治责任是广义而言），文化责任更重。对包括中共和自由派在内的中氤野各政治团体和势力，儒家只有指导教化、诲人不倦的义务，没有枉尺俯就、自轻自贱的权力。<br />
<br />
对自由派这个边缘群体，东海十年来热心过度、参与过多、“失言”过频，该是到了告别的时候了---当然这个告别针对的是自由派群体，不影响个人私交，朋友照样是朋友，不论文化立场如何。<br />
<br />
过门不入真遗憾，改制能持亦可钦。这里我送给自由派的一副对联，算是告别词吧，大意是：认识东海却悟不入良知之妙，经过儒门却登不上大雅之堂，深为遗憾，并暗寓“不憾乡愿”之意（对于乡愿人物，避之为吉，何憾之有）；尽管与儒无G，只要能够坚持民主追求、致力制度更新，仍然是值得敬佩的。<br />
<br />
过门不入典出《孟子-M心下》所引孔子语：“过我门而不入我室，我不憾焉者，其惟乡原乎！”可见除了乡愿，有人过其门而不入室，孔子都会引以为憾。“憾焉者,非憾其人,乃自憾启发有未至也.此自圣人诲人不倦之意”（《法言义疏》）。<br />
<br />
汉赵岐注：“憾，恨也。人过孔子之门不入，则孔子恨之，独乡原不入者无恨心耳。以其乡原贼德故也。”将憾作恨解，没有问题，但要注意，这里的恨，不作怨恨、愤恨、仇恨、痛恨解，而是内心不安之意，如“抱恨”之恨、恨铁不成钢之恨。自由志士向往西方制度是自然的，但盲目崇拜西方文化以及基督教，自断文化之根，自伤心性之本，不仅自误而且误事误世误导民众，岂不可憾！ <br />
<br />
有憾不等于怨恨和厌恶，杨子《法言》：“孟子疾过我门而不入我室.或曰:亦有疾乎?曰:摭我华而不食我实。”这就误解了孔孟。孔孟厌恶的是乡愿而非一般“过我门而不入我室”者。<br />
<br />
四<br />
我与孔孟一样，为“见东海而不入儒门”、“摭我华而不食我实”者深感悲哀和遗憾。一个人不能体悟道德良知的奥妙，可憾；自由派作为一个“致良制”的先锋队，不能认识中华文化的重要性先进性和真理性，甚而以反儒家为光荣，以反道德为先锋，更为可憾。<br />
<br />
