&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;《中苏友好同盟互助条约》<br />
<br />
1949 年12月12日，中华人民共和国主席毛泽东抵达莫斯科进行正式访问。1950年1月20日，中国总理兼外交部长周恩来也抵达莫斯科，两国政府之间的谈判于 2月14日结束。于1950年2月14日签定的条约，同年4月11日起生效，有效期为30年。《中苏友好同盟互助条约》原文如下：<br />
<br />
中华人民共和国-苏维埃社会主义0共和国联盟友好同盟互助条约<br />
<br />
中华人民共和国中0央0人民政府与苏维埃社会主义0共和国联盟最高苏维埃主席团具有决心以加强中华人民共和国与苏维0埃0社会主义0共和国联盟之间的友好与合作，共同防止日本帝国主义之再起及日本或其他用任何形式在侵略行为上与日本相勾结的国家之重新侵略；亟愿依据联合国组织的目标和原则，巩固远东和世界的持久和平与普遍安全；并深信中华人民共和国与苏维埃0社会主义共0和国联盟之间的亲善邦交与友谊的巩固是与中苏两国人民的根本利益相符合的；为此目的，决定缔结本条约，并各派全权代表如左：<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; 中华人民共和国中0央0人民政府特派中国政务院总理兼外交部部长周恩来； <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;苏维埃社会主义0共和国联盟0最0高0苏维埃主席团特派苏联外交部部长安得列．扬努阿勒耶维赤．维辛斯基.<br />
