													<h2>《中苏友好同盟条约》与《中苏友好同盟互助条约》之比较</h2>
						
						
																											<div id="postmessage_814089" class="t_msgfont"><a href="http://jds.cass.cn/Article/20051029134849.asp" target="_blank">http://jds.cass.cn/Article/20051029134849.asp</a><br />
作者：中国社会科学院 栾景河<br />
<br />
中苏两国1950年2月14日签订的《中苏友好同盟互助条约》是新中国成立后对外签订的第一个双边关系条约。该条约的签订反映了人民革命胜利后，中华人民共和国与苏联之间出现的完全新型关系，标志中苏关系从此走向一个全面合作的历史时期。当代史学界对《中苏友好同盟互助条约》的讨论已经相当不少，但仍有许多问题有待深入研究。这不仅关系到对新中国成立后中苏关系性质的界定，而且对我们客观研究新中国成立后中苏关系由同盟互助走向敌对，再到正常化的这段历史具有积极意义。本文力求以两国1945年8月14日签订的《中苏友好同盟条约》为前提，围绕上述两条约的签约背景、主要谈判过程，以及条约文本和附件内容进行一次量化比较，为我们再次客观认识《中苏友好同盟互助条约》的意义，提供一个新的视角。<br />
<br />
一&nbsp;&nbsp;<br />
<br />
首先，讨论1945年8月14日中苏两国签订《中苏友好同盟条约》的背景，我们就不能不对“十月革命”后中苏关系的状况有一个基本的认识。众所周知，以1924年5月31日签订的《中俄解决悬案大纲协定》和《暂行管理中东铁路协定》为基础，中苏两国恢复和建立了新的外交关系，实现了双边关系正常化。但发生在1928年前后的中东路事件，使中苏关系再度恶化，导致苏联政府在1929年7月18日单方面宣布与中国断绝外交关系。尽管中苏双方在后来的时间里做出多种外交努力，但两国关系一直处于非正常状态。[①]“九一八”事件的发生为中苏复交带来了新的契机，苏联根据自身战略利益的需要，对日本侵略中国东三省行为极为关注，向中国给予了道义上的同情与援助。与此同时，由于对国际联盟、美英等大国干涉和制止日本侵略行径的希望彻底破灭，特别是在中国国内要求与苏联复交的情绪的压力之下，国民党政府于1932年10月5日决定同苏联无条件复交。中苏两国在1932年12月12日通过外交换文，重新建立了外交关系，决定互派大使。[②]尽管中苏两国关系此间在形式上恢复了正常，但彼此双方相互充满了不信任和猜疑。“卢沟桥”事件发生后，中日战争全面爆发，苏联政府从自己在远东的战略利益出发，为牵制蒋介石政府与日本妥协，决定援助中国，因此更感兴趣在法理上与中方签订互不侵犯条约，强化对华援助的政治基础。蒋介石之所以在签订《中苏互不侵犯条约》问题上举棋不定，反对把苏联对华军事援助和签订互不侵犯条约联系起来，所担心的就是条约中有侵犯中国主权的内容。中苏两国最终迫于形势的需要，于1937年8月21日签订了互不侵犯条约。客观地看，《中苏互不侵犯条约》的签订在很大程度上推动了中国抗日战争的进程，加强了中苏两国在战时情况下的军事与经济合作，但该条约并未能帮助中国真正解决抗日战争的根本问题，即打败日本的侵略，同时也没有为两国间存在的各种问题达成谅解。苏联尽管以该条约为法律基础，向中国提供了比以往更多的经济贷款和军事物资援助，但此间的中苏关系是极其动荡的。[③]1941年12月7日爆发的太平洋战争不仅是第二次世界大战的一个重要转折点，同时也使未来的中苏关系走向了一个以极大满足苏联利益为前提的历史时期。由于美英中三方均对日本宣战，因此各方要求苏联同时对日宣战。尽管苏联以对德作战仍未结束，不易分散力量为借口拒绝上述要求，但此时世界反法西斯统一战线已经建立，因此苏联表示一旦在远东积蓄力量，时机成熟时即对日宣战。苏联对日宣战对盟国和中国来讲自然是件好事，但令美英两国所预想不到的是，苏联对日宣战的条件是以在远东得到更大的利益为前提。1945年2月间，在苏美英三国首脑举行的讨论欧洲问题的雅尔塔会议上，斯大林明确提出了对日宣战的条件：（1）维持外蒙古（蒙古人民共和国）现状。（2）恢复1904年日本背信弃义进攻所破坏的原属俄国的各项权利，即：（甲）将库页岛南部及其毗连全部岛屿归还苏联；（乙）大连商港国际化，并保证苏联在这个港口的优惠权益，恢复租借旅顺港为苏联海军基地；（丙）设立中苏合营公司，共同经营通往大连的中东铁路和南满铁路，并保证苏联的优惠权益，而中国保持在满洲的全部主权。（3）千岛群岛须交于苏联[④]。尽管美英在某些问题上并不完全赞同苏联的建议，但为尽快使苏联对日宣战，美国几乎是完全满足了苏联的要求。虽然三大国首脑统表示有关外蒙古和上述港口和铁路的协议尚需征得中国国民党政府的同意，但他们同意苏联这些要求应在战败日本后毫无条件地予以满足。苏联方面同时表示准备和中国国民政府签订一项苏中友好协定，以期用武力帮助中国达到从日本枷锁下获得解放的目的。《雅尔塔协定》不仅将战后各国在远东势力范围进行了明确划定，同时也为《中苏友好同盟条约》的签订打下了伏笔。首先，美英两国在苏联远东问题上必须根据协定的有关决定，劝说中国，支持苏联在中国获得相应的利益；其次，对中国政府而言，苏联出兵的前提条件是同意外蒙古独立，同意苏联在中国旅大地区，中东路地区享有种种特权，这也意味着《中苏友好同盟条约》签订的谈判势必只有在《雅尔塔协定》框架的范围内进行。 <br />
<br />
二<br />
<br />
与《中苏友好同盟条约》相比，《中苏友好同盟互助条约》签约背景对刚刚夺取新中国政权的中国共产党人而言极为有利。1949年新中国的成立，标志着中苏关系走向一个新的历史时期。从意识形态的角度来看，中苏两党都是信仰马克思、列宁主义的无产阶级政党。尽管在中国革命进程中，联共（布）、共产国际曾犯下了被部分中共领导人认为是不符合中国国情的指挥性错误，[⑤]但客观地看，联共（布）、斯大林对中国革命的发展与壮大在原则上是给予了积极支持，中苏两党高层在人民解放战争时期保持着密切联系。[⑥]随着中国国民党政权的垮台，中国共产党人领导的革命取得了胜利，令苏联方面感到与中国国民党政府签订的《中苏友好同盟条约》让中国共产党人继续接受行未必适宜，而且联共（布）中央、包括斯大林本人深知该条约对中国方面的不平等性，并且表示在适当时机对该条约的有关条款加以修改。[⑦]毛泽东在新中国成立仅两个月后的12月26日就率中国政府代表团匆忙出访苏联的目的，表面上看是为参加斯大林的70寿辰，但其真正用意是为同苏联签订一个新的条约，既在政治上得到苏联的强有力支持，在经济上获得苏联的各项援助。苏联方面之所以此时同意毛泽东访问莫斯科，是因为中共在国内已经夺取全国政权，苏联无论从革命的利益出发，还是从地缘战略的角度显然希望同中国共产党领导下的中国保持一种友好关系。苏联此时再也不需要像以往那样在对华关系问题上与中共和国民党政府保持一种双重的外交关系。[⑧]纵观此间国际局势，以美国和苏联为首的两大阵营已经形成，中共在长期的革命斗争实践和历史教训中打消了在苏美之间保持平衡、做美苏桥梁、“中立美国”等想法，而采取了反蒋必须反美的政策。[⑨]中共的上述做法正是处于对战后国际关系发生新变化的战略调整，而这种战略恰好符合苏联的革命利益，换言之，符合苏联在远东的战略利益。面对战后国际社会的新格局，不仅新中国需要苏联作为一个强大的军事、政治、经济后盾，苏联更需要中国作为其远东的战略屏障，与帝国主义阵营抗衡。有关毛泽东计划访苏的历史，我们可以追述到解放战争时期。早在新中国成立之前，毛泽东就曾至少三次计划访问苏联，但由于中国复杂的战争局势迫使毛泽东的访苏计划一再拖延和耽搁。尽管在此期间中苏两党保持着友好关系，但苏联同蒋介石国民党政府保持的外交关系并未中断，因此令苏联方面感到棘手。从现已公布的档案文献中完全可以得到证实，米高扬1949年1-2月间秘密访问中共中央所在地西柏坡涉及了新中国成立后中苏关系应如何确立，而刘少奇1949年6-8月间秘密访问莫斯科强化了新型中苏关系的原则，明确了毛泽东到访莫斯科的时间，即毛泽东在1949年底，中苏两国建立外交关系后正式访问苏联。[⑩]1949年 11月9日，毛泽东以中共中央名义就其访问莫斯科的时间向中国驻苏联大使王稼祥通报。次日，周恩来在同苏联驻中国大使罗申的会谈中正式向苏联方面提出毛泽东访问苏联的计划。毛泽东访问莫斯科的目的是希望参加斯大林70 寿辰的庆典活动，向斯大林亲自表示祝贺。除同斯大林建立私人联系外，毛泽东在访问期间还准备提出签订中苏条约问题。”[11]苏联方面对毛泽东此次提出访问苏联的计划给予了及时答复，表示“欢迎中国代表团到莫斯科”。[12] <br />
