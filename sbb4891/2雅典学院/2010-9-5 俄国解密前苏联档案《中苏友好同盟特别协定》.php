<br />
双方并宣布愿以忠诚的合作精神，参加所有以确保世界和平与安全为目的之国际活动，并为此目的之迅速实现充分贡献其力量。<br />
<br />
第二条<br />
<br />
缔约国双方保证经过彼此同意与第二次世界战争时期其他同盟国于尽可能的短期内共同取得对日和约的缔结<br />
<br />
第三条<br />
<br />
缔约国双方均不缔结反对对方的任何同盟，并不参加反对对方的任何集团及任何行动或措施。<br />
<br />
第四条<br />
<br />
缔约国双方根据巩固和平与普遍安全的利益，对有关中苏两国共同利益的一切重大国际问题，均将进行彼此协商。<br />
<br />
第五条<br />
<br />
缔约国双方保证以友好合作的精神，并遵照平等、互利、互相尊重国家主权与领土完整及不干涉对方内政的原则，发展和巩固中苏两国之间的经济与文化关系，彼此给予一切可能的经济援助，并进行必要的经济合作。<br />
<br />
第六条<br />
<br />
本条约经双方批准后立即生效，批准书在北京互换。<br />
<br />
本条约有效期间为三十年，如在期满前一年未有缔约国任何一方表示愿予废除时则将延长五年，并依此法顺延之。<br />
