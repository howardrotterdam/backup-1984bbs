													<h2>俄国解密前苏联档案《中苏友好同盟特别协定》</h2>
						
						
																											<div id="postmessage_813910" class="t_msgfont"><a href="http://comewithmaple13.blog.163.com/blog/static/1695293472010816564410/" target="_blank">http://comewithmaple13.blog.163. ... 293472010816564410/</a><br />
该条约由中国总理周恩来和苏联外长安・扬・维辛斯基在莫斯科克里姆林宫签署。是《中苏友好同盟互助条约》的非公开版本，而公开的《中苏友好同盟互助条约》内容共有六条，最大的特点是承认外蒙古0独0立，毛泽东自己当年也称签定此约是“丧权辱国”。60年代起，幸好中苏两国关系恶化，该条约名存实亡。期满后没有再延长。 <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;从条约所订六条看，除中共和苏共政权结成军事同盟，将自己置于苏共保护之下外，好象没有太大问题，而且有些问题也说的不是那么清楚，那为什么毛泽东将其说成是“丧权辱国”的卖国条约呢？其实毛泽东的话是另有所指。就在这个条约签定的前两天，也就是2月12 日，毛泽东同苏共斯大林签定了另外一个《特别协定》，那才是名符其实的丧权辱国的卖国条约，清朝所签定的所有卖国条约，都是在战败的情况下，不得已而签定的城下之盟，这个条约却是在没有任何外部压力下主动送上门去的。由此，我们可以理解中共为什么能在中国夺取政权，以及为什么在建政初期实行一面倒的外交路线。 <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;1950 年2月12日中共同苏共所签定的条约叫《中华人民共和国苏维埃社会主义0共和国联盟友好同盟特别协定》，这个《特别协定》是我们解读《中苏友好同盟互助条约》的钥匙，时间上只差两天，内容涉及到了重大实质性问题，可以说，《中苏友好同盟互助条约》只是为了更体面地向外宣布，是欺骗世人的幌子，《特别协定》才是要执行的条约。这个《特别协定》本来是对外保密的，但是在1950年7月16日，被美国对外政策协会在纽约公布。通过这一密约，人们不难看出，苏联人已经把中国拉入它的整个战略体系中，并且在国家的政治、军事、经济、贸易、工矿、交通、资源等一系列重要领域，完全控制了中国，让中国民众在苏联征服亚洲以及整个世界的共产主义运动中当前锋、打头阵，充当牺牲品。<br />
<br />
&nbsp; &nbsp;《特别协定》的中国方面的主持人，以毛泽东为首、以周恩来为全权代表而签约。苏联方面的主持人，以斯大林为首、维辛斯基出面签订。有以下内容值得注意： <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;首先在军事上，《特别协定》规定：“中华人民共和国允许苏维埃社会主义0共和国联盟驻兵中国境内，以共同保卫世界和平。中华人民共和国先行划交东北、华北海空基地予苏维埃社会主义0共和国联盟，作军事上之部署。将中国人民解放军改编为国际红军，由红军最0高0统帅直接指挥。”这就完全控制了中国的军事。特别应该指出的是，苏俄在协定中还要求中共负责筹集华工一千万人，协助苏联，共同建设中苏军事设备，以应付帝国主义之侵略行动。增加兵额四百万，以准备帝国主义侵略行动。在后来的韩战中，中国不得不派几百万的志愿军参战，为苏俄的世界霸权充当炮灰，都同这个协定有着直接关系。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;第二，《特别协定》中规定：“中华人民共和国之人口，因目前资源缺乏，非减少一亿，决不能支持，其详细办法，由中华人民共和国自行定之”中国人的生命，完全受苏俄控制，苏俄对中国人生命有生杀大权。虽然以后发生的“大镇压”、“三反五反”、“反右”、“大饥荒”等不一定是照苏俄指示行事，但这个协定的影响也是不能忽略的。抗美援朝中又不惜中国人性命以人海对付联合国的先进部队，后来毛泽东又多次叫打大仗，打核大战，牺牲中国一半以上人口来战胜帝国主义，都表现了毛泽东想用战争来减少人口的思路。 <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;第三，在政治上，协定规定&quot;中华人民共和国中央人民政府所属各机关，公营事业，应设苏维埃社会主义0共和国联盟专门人员为顾问。&quot;完全操控中国的内政，实际上把中国当作了他们的占领区。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;第四，在工业上苏俄派大批所谓技术人员控制中国的工业，直接操作中国工业的经营管理，对这些人员政府还要给以优待的 &quot;供给制&quot;予以优待。中国还要对苏俄开放沿海商阜，内陆市场，由苏俄 &quot;自由通商&quot;，税率只有百分之一，就是在自己国家，也不可能享受到如此优厚的税率待遇。协定还规定，苏联得&quot;享受贸易特权，中华人民共和国应以谷物供应苏联政府。&quot;在当时中国是个缺粮国家，这个协定的前边说，&quot;中华人民共和国之人口，因目前资源缺乏，非减少一亿，决不能支持&quot;。由于中共发动战争的原因，中国当时粮食大面积减产，粮食产量不到战前的四分之三，棉花几乎减产一半。但是中国必须先喂饱苏俄，中国人自己才能吃饭。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;第五，协定规定苏俄有&quot;支配中华人民共和国境内之矿铁原料等特权。其中以锡矿，全年产量除留百分之二十自用外，余需供应苏维埃联盟，发展重工业&quot;。中国国内的矿产资源中国自己不能支配，要首先支援苏俄发展工业，这种赤裸裸的掠夺行为，同侵略者有什么两样？<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;第六，中国境内的北京、天津、上海、广州、重庆、长沙、杭州、九江、芜湖、厦门、汕头、福州等十五个城市的中心地区，要作为苏维埃联盟侨民居留地。实质就是把这些地区变作苏俄殖民地。特别应该注意的是，苏俄想占有的是这些大城市&quot;中心地区&quot;，完全是以主子自居，把中国人当作他们的奴隶。 <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;第七，协定中规定：&quot;缔约国双方同意，内蒙，新疆，西藏，建立各民族的人民共和国，由双方共同负责扶助其独立。&quot;这个规定显示了中国领土主权的丧失，苏俄有对中国领土的处置权。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;正是在这个秘密协定的主导下，中国成了苏俄的附属国，中国的国民沦为受人压迫的二等公民。苏俄打着支援中国的幌子，明正言顺地掠夺中国资源。中国人对此稍有不满，就会被打成反苏、反党反社会主义0而被投入监狱，有的被迫害致死。不管中国国情，完全按苏俄的意愿规划中国社会制度，刘少奇在一九五四年关于宪法草案的报告中说：&quot;苏联的道路是按照历史发展规律为人类社会必然要走的道路。想要避开这条路不走，是不可能的。&quot;毛泽东在1953年2月举行的人民政治协商会议第四次会议上讲话说：&quot;要认真学习苏联的先进经验。无论共产党内、共产党外、老干部、新干部、技术人员、知识分子，以及工人群众和农民群众，都必须诚心诚意地向苏联学习。&quot; <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;在中外历史上，曾出现过许多出卖国家利益的政权，也出现过很多丧权辱国的卖国贼，但没有任何一个政权倾全国之力甚至民众的生命去为别的国家作牺牲，整个 50年代的历史，就是中国沦为苏俄奴隶的历史，在那个时代，也许你打死一个中国人无罪，但你要对苏俄人稍有不满或提一点意见，你就会受到灭顶之灾。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;现在我们看当年发行的纪念《中苏友好同盟互助条约》的邮票，所应关注的应该是这组邮票反映的背后真实的历史事件。我们千万不要忘记，在两天的时间里，有两个完全不同的条约，一个是用来对外公布的《中苏友好同盟互助条约》，一个是用来真实实行的密约《特别协定》。在那个友好条约幌子后面，记载着中国人真正的历史。<br />
<br />
-------------------------------------------------------------------------------------------------------------------------------------------------------<br />
<br />
1950 年2月12日，在莫斯科，中苏双方缔结这一秘密&quot;协定&quot;，中国方面的主持人，以毛泽东为首、以周恩来为全权代表而签约。苏联方面的主持人，以斯大林为首、维辛斯基出面签订。这项秘密协定，几乎完全顺从和维护苏联统治者的意旨和权益。全文如下：<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;附录一：<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 《中华人民共和国苏维埃社会主义0共和国联盟友好同盟特别协定》<br />
<br />
中华人民共和国中央人民政府与苏维埃社会主义0共和国联盟中华人民共和国苏维埃社会主义0共和国联盟友好同盟特别协定0最0高0苏维埃主席团，为加强中华人民共和国与苏维埃社会主义0共和国联盟之间的密切合作，共同防止帝国主义用任何形式上的侵略行为，以及勾结日本帝国主义的再起，以建立亚洲新秩序，巩固中苏友好合作关系，特在两国友好同盟互助条约以外，缔结特别协定，并各派全权代表如左：<br />
<br />
中华人民共和国中央人民政府特派中国政务院总理兼外交部长周恩来<br />
<br />
苏维埃社会主义0共和国联盟0最0高0苏维埃主席团特派外交部长安德列（努阿勒耶维赤）维辛斯基<br />
<br />
两全权代表互相校阅证书认为妥善后，同意下述各条：<br />
<br />
第一条：缔约国双方为共同防止帝国主义之侵略，及共同应付第三次世界大战，中华人民共和国允许苏维埃社会主义0共和国联盟驻兵中国境内，以共同保卫世界和平。<br />
<br />
第二条：自缔约日起，中华人民共和国先行划交东北华北海空基地予苏维埃社会主义0共和国联盟，作军事上之部署，并由中国人民解放军负责协助进行东南亚的解放事业，以完成亚洲解放大业。<br />
<br />
第三条：缔约国双方同意，将中国人民解放军改编为国际红军，由红军0最0高0统帅直接指挥。<br />
<br />
第四条：中华人民共和国，负责筹集华工一千万人，协助苏联，共同建设中苏军事设备，以应付帝国主义之侵略行动。<br />
<br />
第五条：中华人民共和国应将华北各口岸开放予苏联永久驻兵，并自由出入，其中包括秦皇岛，海州，烟台，威海卫，青岛，大连。<br />
<br />
第六条：中华人民共和国，在本年底以前增加兵额四百万，以准备帝国主义侵略行动。<br />
<br />
第七条：中华人民共和国之人口，因目前资源缺乏，非减少一亿，决不能支持，其详细办法，由中华人民共和国自行定之。<br />
<br />
第八条：中华人民共和国中央人民政府所属各机关，公营事业，应设苏维埃社会主义0共和国联盟专门人员为顾问。<br />
<br />
第九条：缔约国双方同意，苏维埃政府调遣技术人员参加中国各地主要工业的经营，中华人民共和国政府应以优待的&quot;供给制&quot;予以优待。<br />
<br />
第十条：中华人民共和国沿海商阜，内陆市场，开放予苏联自由通商，并以百分之一的优惠条件为税率。<br />
<br />
第十一条：缔约国双方同意，中苏在互惠互利条件下，进行物物互相交换，以建立友好关系。<br />
<br />
第十二条：苏维埃联盟政府，有支配中华人民共和国境内之矿铁原料等特权。其中以锡矿，全年产量除留百分之二十自用外，余需供应苏维埃联盟，发展重工业，协助建设中华人民共和国之工业化。<br />
<br />
第十三条：中华人民共和国境内之北京、天津、上海、广州、重庆、长沙、杭州、九江、芜湖、厦门、汕头、福州等十五都市，划定中心地区，作为苏维埃联盟侨民居留地。<br />
<br />
第十四条：苏维埃社会主义0共和国联盟政府，为满足中华人民共和国中央人民政府请求，贷款三万万美元给予中国（贷款支配偿还原则，有贷款协定订明）唯中华人民共和国须将东北华北两地之全部原料产品作为使用抵押，偿还时之原料种类，由苏维埃联盟，视其实际需要而决定之。<br />
<br />
第十五条：缔约国双方同意苏联政府，共同管理长春铁路，及沿路两旁五十华里之地区。双方代表所担负的职务如铁路局长，理事主席，顾目前事实需要，须由苏联代表担任，中国代表副之。<br />
<br />
第十六条：依前中国共产党与苏维埃社会主义0共和国联盟订立之满洲协定，苏联得继续享受贸易特权，中华人民共和国应以谷物供应苏联政府。<br />
<br />
<strong>第十七条：缔约国双方同意，内蒙，新疆，西藏，建立各民族的人民共和国，由双方共同负责扶助其独立。</strong><br />
<br />
第十八条：协定约经双方批准后，立即生效，批准书在赤塔互换。<br />
<br />
<strong>第十九条：协定约系机密性质，缔约国双方均有义务保守秘密，不得公布。</strong><br />
<br />
1950年2月12日订立于莫斯科。共两份，每份均以中文俄文书就，两种文字之条文，均有同等效力。<br />
<br />
中华人民共和国中央人民政府全权代表 周恩来（签名）<br />
<br />
苏维埃社会主义0共和国联盟0最0高0苏维埃主席团全权代表 安(扬) 维辛斯基（签名）<br />
