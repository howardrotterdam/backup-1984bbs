三<br />
<br />
《中苏友好同盟条约》与《中苏友好同盟互助条约》不仅签约背景有明显区别，而且在谈判内容上同样巨大差异。前者是在苏联方面巨大压力下进行的，而后者则具有明显的协商性，而这种差异直接影响到了后来签订的条约内容。在此之前，国民党方面对签订条约谈判均持乐观态度，甚至美国一再向中方建议应尽力在悬而未决的问题上与苏联达成正式协议，而且目前是中苏之间达成协议的最好时机。1945年6月30日，在中国行政院院长宋子文率团抵达莫斯科拜见斯大林的过程当中，斯大林就已经暗示中方，协议肯定是要达成的，但能否出兵东北，签订条约，首先要看中国方面能否满足苏联的要求。为此，斯大林提醒宋子文，谈判的过程应当是艰苦的，要宋子文对此坚定信心，而且不要有丝毫疑虑。[13]的确如此，在7月2日的正式会谈中，斯大林首先为谈判的前提划定了范围，那就是中方对《雅尔塔协定》内容了解的程度和渠道来源。在其确信中方确实了解该协定的基本内容后，斯大林同意进行谈判，而所谈及的内容首先是外蒙古独立问题。宋子文以蒋介石认为外蒙古问题不是目前急于解决的问题为由表示暂不予讨论，但斯大林坚持要求首先要解决外蒙古问题。在斯大林看来，外蒙古作为苏联在远东地区的安全屏障具有特殊战略意义。如果苏联在外蒙古无自卫之法律权，苏联将失去整个远东。[14]斯大林认为，即使德日两国无条件投降，但两国均是强国。正是基于上述考虑，苏联在外蒙古必须享有自卫的法律权利。斯大林认为，外蒙古人民既不愿意加入中国，也不愿意加入苏联，而是要求独立。对中国而言，同意外蒙古独立是利大于弊，反之对中苏双方均属有害。斯大林强调，苏联建议与中国结为同盟的目的是加强苏联对日本的战略地位，以两国的军事力量，再加上美英的力量永远战胜日本，其之所以提出对旅顺、大连、库页岛南部以及外蒙古的要求目的同样是加强反对日本的战略地位。[15]<br />
<br />
关于大连问题，宋子文指出：“《雅尔塔协定》关于1904年因受日本诡诈攻击所害之俄方昔日权益应予恢复”的声明似乎过于笼统，要加以分析。但斯大林解释这指的是并非对中国方面的权益，是针对苏联与日本的关系。斯大林要求在港口的管理上，特别是苏联使用的港湾的条件上应比1898年条约[16]规定的有所改善。如果能以该条约为基础，及以改善，是件大好事。宋子文表示，港口的管理权应属中国，但因苏联是最大的航运国家之一，所以优越商业地位应属中苏两国。宋子文强调，该条约是同帝俄政府所签订的，加上目前期限已满，而且不存在当年的战争形势，因此不应该以该条约为基础。斯大林反驳道，不仅需要签订新的条约，而且还要更多的权益，不仅是对各国开放，而且要保证苏联在港口的优越地位。斯大林明确地提出，苏联需要一个不冻港，如果苏联对中东铁路所经过的港口没有若干权力，要中东路还有什么用。[17]关于旅顺军港问题，宋子文表示不愿意再有租借地，希望中苏两国共同使用港口。斯大林表示，尽管理解中方的请求，而且不愿意做有害中国的先例，但可以找到更合适的词语代替，旨在达到自己的目的。关于中东路问题，斯大林认为，该铁路为旅顺军港使用，也为海参崴与西伯利亚建立联系，中苏双方共同经营，所有权应归铁路的建设者（即苏联―作者），将来归中国所有，期满后苏联立即撤出该铁路。<br />
<br />
应当指出，正是在此次会谈中，双方提到了中国的内政，自然包括了国民党与中共的关系问题，以及苏联在该问题上的立场。斯大林认为国共关系问题是中国的内部问题，他之所以感兴趣是因为关心中国的前途。这里所要强调的是，正是斯大林的上述立场促使了国共之间的再度谈判，并于1945年10月10日签订了《双十协定》。客观地说，这次谈判是中国革命史中的一个重大事件，但也让斯大林永远背上了不须革命的“黑锅”。[18]面对斯大林咄咄逼人的态度，宋子文在当日致蒋介石的电报中称：“本日谈话时，对东三省比较满意，外蒙问题则成僵局。”[19]宋子文将斯大林提出的外蒙古独立问题，旅大的租借期限问题和对中共的态度问题向蒋介石做了详尽汇报，请蒋介石指示在斯大林坚持其主张的情况下如何处理。7月5日国民党中央举行高级官员会议对中苏谈判的条件做出决定。次日，蒋介石把会议决定的主要内容电传宋子文，要求其在谈判中切实交涉：（1）东北三省的领土、主权和行政必须完整。旅顺军港的行政管理权必须由中国主管，军港可以和苏联共同使用，但不是共同管理。大连为自由港，按照各国自由港管理的范例，但行政权由中国主管。中东路可以同苏联共同经营，但不是双方共同管理。（2）新疆的伊犁和整个新疆沦陷区必须完全恢复，中苏双方应依照以往的协定共同在边界地区清剿土匪。阿尔泰地区应属新疆。（3）中共对军令，政令必须完全由中央统一指挥，按照各国政党和国家法令的原则，政府将一视同仁，正式召开国会。政府改组时可容纳中共进入行政院。（4）中国必须统一到如此程度才可以按三民主义原则主动提出外蒙古，计划由外蒙古人民用投票的方式解决。如果投票结果是外蒙古独立，那么政府将正式提请国会正式通过后，方可宣布独立，但必须是在抗战结束之后。<br />
<br />
1945年7月9日，在中苏双方举行的最重要的第四次会谈中，宋子文首先宣读了蒋介石的电报指示：“中国政府今愿以最大牺牲与诚意，寻求中苏关系根本之解决，扫除今后一切可能之纠纷与不快，籍获两国彻底之合作，以完成孙总理在日与苏联合作之遗志，中国最大之需要为求领土主权行政之完整，与国内真正之统一，于此有三项问题切盼苏联政府予以充分之同情与援助，并给以具体而有决心之答复。”蒋介石要求苏联答复的问题如下：（1）满洲领土主权与行政完整。为了中苏共同的利益出发，中国准备共同使用旅顺军港，大连辟为自由港，期限为20年，旅顺、大连的行政管理权应属于中国，目的是保证中国在满洲的主权真正完整。中东、南满铁路干线可以与苏联共同经营，利润平均分配，但铁路所有权应属中国，铁路支线及铁路本身以外的经营不包括资共同经营范围之内，期限为20年。（2）新疆在最近一年内发生叛乱，导致中苏交通中断，商业贸易无法维持，希望苏联能按照从前签订的协定，与中方一道平定叛乱，使得贸易交通得以恢复。阿尔泰山脉原属新疆，应该继续是新疆的一部分。（3）中国共产党有自己单独的军事和行政组织，由于其军令和政令未能归中央统一，深盼苏联只对中央政府予以所有精神和物资上的援助，苏联政府向中国提供的一切援助应以中央政府为限。蒋介石在提出上述三个要求外表示；“中国政府以外蒙问题既为中苏两国关系结症之所在，为中苏共同利害与永久和平之计，愿于击败日本及上述三项有苏联政府接受之后，准许外蒙之独立，为避免纠纷起见，拟采取公民投票方式，投票以后中国政府当宣布外蒙之独立。”[20]蒋介石同时在电报中对对外蒙古的疆界范围做出明确划定，即以中国旧地图为准。斯大林听完蒋介石的电报内容后，对宋子文以蒋介石电报为基础进行谈判的方式颇为满意。尽管中苏双方在外蒙古的独立、其主权和宗主权问题上的理解和看法有很大分歧，但这丝毫没影响谈判的继续。斯大林做出承诺：关于满洲问题，他已宣称，并愿意做出任何中方希望得到的声明，苏联完全承认中国在满洲的主权。关于中国共产党，斯大林表示并不予以支持，也没有支持的意向。他认为中国只有一个政府，如果中国国内还有另一个政府，自称为政府，应当由中国自己去解决。关于蒋介石提出的只援助中央政府的要求，斯大林表示自己从来都是这么做的。斯大林表示，自己愿意同中国真诚相处，保持同盟国之间的应有关系。在8月12日的第九次会谈中，中苏双方就一系列技术性问题进行磋商，苏方继续坚持在中长铁路局局长和军事委员会问题上以往的立场，强调旅顺市区的行政不能划归中国的原则，令中方难以接受，但中方谈判代表宋子文已经明显感到苏方已不可能妥协。8月13日，蒋介石致电宋子文称：“对于外蒙及其他未决事项，准授权兄等权宜处置可也。”[21]有了蒋介石的授权，8月13日深夜，斯大林和宋子文、王世杰终于最后就争议问题达成一致。至此，所有问题获得解决。 <br />
<br />
四<br />
<br />
毛泽东一行1949年12月16日抵达莫斯科后，即与斯大林进行了首次会谈，所涉及的首先是如何对待《中苏友好同盟条约》问题。毛泽东表示，中共中央曾讨论了《中苏友好同盟条约》。斯大林认为这个问题可以讨论解决，但需要澄清应当宣布保留1945年缔结的现有中苏友好同盟条约，还是应当声明将它进行修改，或现在就对它做相应的修改。斯大林不无担心地表示苏中之间的这个条约是根据“雅尔塔协定”缔结的，而该条约的主要条款是经过美国和英国同意的。考虑到这个情况，苏方在自己的小范围内已决定对这个条约暂不做任何修改。斯大林建议可以在形式上保留而实际上修改这个条约，即苏联在形式上保留在旅顺口驻军的权利，但根据中国政府的建议撤退驻在那里的苏联军队。至于中长铁路，在这种情况下，可以形式上保留，而实际上根据中国方面的愿望修改协定的有关条款。斯大林以协商的口吻对毛泽东说：如果这种做法不能使中国同志满意，那么你们可以提出自己的建议。[22]毛泽东表示：我们在中国讨论条约时，没有考虑到美国和英国在雅尔塔协定问题上的立场。怎样有利于我们的共同事业，我们就应当怎样做。这个问题应当考虑。现在已经清楚，目前不应当修改条约。[23]<br />
<br />
关于中国长春铁路和旅顺口问题，毛泽东表示中长铁路和旅顺口目前的状况符合中国的利益，因为单独一个中国的力量不足以抵御帝国主义的侵略。此外，中长铁路还是培养中国铁路干部和工业干部的学校。斯大林回答说，撤军并不意味苏联不再援助中国。斯大林同时对苏联军队驻扎在中国领土有可能引起美英等国反对表示了担忧。他强调如果苏联军队根据双方同意由旅顺口撤出，那么中苏两国在国际关系中将是赢家。大家将会看到中国共产党人做到了蒋介石没有能做到的事。斯大林表示，虽然条约赋予苏联在旅顺口驻军的权利，但苏联可以不使用这个权利，根据中国政府的请求退出军队。相反，如果这样做不合适，苏联军队可以根据中国的需要留驻若干年。<br />
<br />
&nbsp; &nbsp; 由此看出，中苏双方在对待是否修改或重新签订中苏友好条约问题上的看法出现分歧。<br />
<br />
苏联方面实际上是采纳了刘少奇1949年7月4日提出的对待中苏友好同盟条约的第一种方<br />
<br />
案，即中国政府发表声明，它完全接受这个条约并使其生效，不做任何修改。[24]毛泽东虽然<br />
<br />
在会谈中表示中方在讨论条约时没有考虑到美国和英国在雅尔塔协定问题上的立场，同意苏<br />
<br />
联方面提出的目前不修改该条约的意见，但在心里上坚持认为该条约是苏联和国民党政府签<br />
<br />
订的，而且国民党已经倒了，不重新签订该条约，不好向国人交待。但有一点是可以肯定的，<br />
<br />
斯大林在整个会谈中没有像对待宋子文那样，向中方施加任何压力。因为在斯大林看来，自<br />
<br />
己所担心的外蒙古问题早在几年前已得到解决，而且目前的国际形势与五年前已大不相同。<br />
<br />
在此期间，为表示对毛泽东到访的重视，斯大林数次派部长会议副主席莫洛托夫、外交部长维辛斯基等人前往毛泽东住处探望，了解毛泽东的愿望和想法；双方应当进一步做些什么，是否有新的考虑。斯大林还亲自打电话给毛泽东询问有什么想法。[25]1950年1月2日晚，毛泽东在同莫洛托夫、维辛斯基和联共（布）中央驻中共代表柯瓦廖夫的会面中表明，中方想同苏联签订一个新条约，使中苏关系在新条约的基础上巩固下来。毛泽东认为中苏之间签订一个新条约，反映了人民革命胜利后，中华人民共和国与苏联之间出现的完全新型关系。对现有条约进行重新审查是完全必要的，因为这个条约的两个重要组成部分--日本和国民党已发生了重大变化；日本已经不再是一种武装力量，而国民党已经跨台。此外，众所周知，中国人民当中的某些人一直对现有中苏条约不满，所以签订一个新的中苏友好同盟条约对双方都是有利的。[26]根据毛泽东1950年1月2日的具体指示，（恩来准备五天，1月9日从北京动身，1月19日到莫斯科）周恩来一行1950年1月10日凌晨由北京动身，1月20日抵达苏联首都莫斯科，就签订中苏友好同盟互助条约及贷款、通商、贸易等问题同苏方进行谈判。正是由于周恩来出访莫斯科的工作做得较为充分，加上毛泽东1月2日、3日在致中共中央电报中对周恩来此次访问的任务加以明确，因此在周恩来抵达莫斯科的第三天，即1月22日，中苏双方就有关起草新条约和贷款等协定的各项原则性问题举行了会谈。<br />
<br />
斯大林在听取了莫洛托夫、米高扬、维辛斯基等人关于中方希望签订新条约汇报后，改变了原来所坚持的对《中苏友好同盟条约》暂不做修改的立场，同意与中方签订新的友好条约。斯大林在2月22日的会谈中首先发言，认为需要讨论的问题可分两类：第一类是涉及苏中之间现有的协定；第二类是涉及满洲、新疆等地的当前事务。关于中苏条约问题，斯大林表示苏联方面认为这个条约应当修改，尽管苏联方面从前曾经想这个条约可以保留，但条约的基础是反对日本的战争，既然战争已经结束，日本已被打跨，形势发生了变化，因此这个条约就成了落后于时代的东西。[27]<br />
<br />
当斯大林请中方谈谈对新约的想法时，毛泽东表示中方还没有具体条约草案，仅有一些设想。斯大林就此回答说：我们可以交换意见，然后再起草相应的条约草案。随后，毛泽东表示：“考虑到目前的形势，我们认为应当通过条约和协定巩固我们两国现有的友好关系。这无论是在中国，还是在国际关系方面，都会有良好的反应。”毛泽东强调在新约中应当把保证我们两国繁荣昌盛的东西固定下来，而且还应当规定必须防止日本侵略的重演。因为我们两国都希望繁荣昌盛，因此不能排除帝国主义国家企图干扰我们。斯大林对毛泽东的建议表示赞同，同时认为日本虽然战败了，但它的骨干保留了下来，它们迟早必定会抬起头来，特别是在美国继续奉行当前政策的条件下。毛泽东再次强调新条约同旧条约的根本区别在于国民党从前仅仅是在口头上讨论友好，而现在情况有所不同，已经具备真正友好合作的一切条件。新条约应当包括政治、文化和军事合作问题，经济合作是最重要的问题。[28]在谈到中长铁路问题时，斯大林征求毛泽东的意见，询问中方有哪些建议。毛泽东表示：“或许，像旅顺口协定那样，应当在法律上保留中长铁路协定，而实际上进行修改的原则作为基础。”斯大林说：“这不是说，你们同意宣布现有协定在法律上仍然有效，而实际上作些适当的修改。”毛泽东认为中方应当考虑从中苏双方共同的利益出发。在斯大林明确表示旅顺口协定是不平等的协定后，毛泽东担心修改这个协定会触及《雅尔塔协定》的原则，会对苏方不利。斯大林则表示既然我们采取了修改条约的立场，那就需要进行到底。当然，这样做会使苏联有些难堪，苏联方面还要同美国进行斗争，但我们对此已经迁就过了。斯大林就此解释道，苏联在抗日战争时期签订现有条约的出发点是苏联军队驻扎在旅顺口将有利于苏联，有利于中国的民主力量。斯大林建议宣布旅顺口协定在缔结对日本和约前仍然有效，在与日本缔结和约后苏联军队撤离旅顺口，或者宣布保留现有协定，而实际上苏联军队从旅顺口撤军。斯大林表示无论采纳何种方案，苏方均表示同意。[29]毛泽东对斯大林提出的建议持保留态度，虽然表示同意斯大林的意见，但希望中苏双方在旅顺口进行军事合作，训练中国的海军。毛泽东认为旅顺口可以成为中苏双方进行合作的基地，而大连则可以成为中苏双方进行经济合作的基地。最后，双方就旅大问题达成一致意见，并决定新条约由周恩来和维辛斯基负责起草。在具体谈到中长铁路问题时，毛泽东认为在新协定中应当指出共同经营与共同管理的原则今后将继续执行，但在共同管理问题上中方应起主要作用。莫洛托夫认为在两国合作和共同管某个企业的条件下，通常规定双方的股份额度均等，以及领导职务轮换制。虽然在旧协定中规定，铁路的管理归苏联方面，但苏联方面认为今后必须实行领导职务的轮换制。斯大林同样认为既然是共同管理，那就必须实行领导职务的轮换制，这样更合乎逻辑。苏方不反对缩短协定的有效期。就此，中苏双方商定有关经济合作问题由周恩来、李富春、米高扬、维辛斯基负责解决。毛泽东对中苏谈判及中苏友好同盟互助条约的起草工作感到满意，并于1950年1月25日致电刘少奇做如下通报：（一）22日，我们连师哲共六人与斯大林等同志会商，决定各项原则问题及工作方法。23日，周王李[30]三人与苏方米高扬、维辛斯基、罗申三人会谈几个具体问题。24日，经我们起草的中苏友好同盟互助条约草案送交维辛斯基。现起草第二个文件，即关于旅顺大连中长铁路协定，大约今日可以完成草案，并已决定在三天内准备好第三个文件，即中苏贸易协定。总之，工作是颇为顺利的。[31]尽管我们目前只见到了毛泽东同斯大林两次会谈的纪要，但从双方会谈的内容和各自发言的情况来看，没有理由认定双方的会谈是不平等和不相互尊重的。 <br />
<br />
五<br />
<br />
《中苏友好同盟条约》与《中苏友好同盟互助条约》在名称上仅两字之差，而且文本的条款极其相似，甚至某些条款确有雷同之处，[32]很容易让人感觉到新约是由旧约演变而来。[33]但当我们仔细比较新旧条约的文本就不难发现，尽管两条约有明显的相似之处，但就其内容和主要原则具有本质区别。<br />
<br />
《中苏友好同盟条约》首先是苏联同中国国民党政府签订的一个军事同盟，其宗旨就是双方担任协同其他联合国对日作战，直至获得最后胜利为止。从条约的前四款来看，该条约的主要目的是针对日作战和防止日本重新发动侵略。而后四条则是关于双方在战后的合作，以及涉及双边关系的原则性立场。尽管条约规定了双方将在和平再建后，“依照彼此尊重主权及领土完整与不干涉对方内政之原则下，共同密切合作。”但可以看到，此时尊重主权和领土完整已是在保证外蒙古独立的情况之下。关于外蒙古问题，在中国政府外交部长王世杰与苏联外交人民委员部部长莫洛托夫的照会中双方再次确定“兹因外蒙古人民一再表示其独立之愿望，中国政府声明，于日本战败后，如外蒙古之公民投票证实此愿望，中国政府应当同意外蒙古之独立，即以其现在之边界为边界。”实际上对外正式宣布了中方在战后同意外蒙古独立。关于中国长春铁路，双方规定，该铁路的所属权归中苏双方共同所有，并共同经营。关于大连问题，协定规定，宣布大连为自由港，对各国贸易及航运一律开放。除此以外，《关于大连协定议定书》还规定了所有港口工事之一半，无偿租与苏方，租期定为30年，其余一半由中国留用。港口的扩建与重建，应由中国与苏联同意。苏联方面为了确保自己在旅大的军事地位，在该协定中要求中长铁路由大连通往沈阳，在旅顺口海军根据地区域以内各段，应不受该区域内所设定之任何军事监督或管制。《关于旅顺口之协定》规定了两缔约国共同使用旅顺口为海军基地，其目的是加强中苏两国的安全，防止日本再事侵略。同时，协定附件明确划定了海军基地的范围。应当肯定是，《关于中苏此次共同对日作战苏联军队进入中国东三省后苏联军总司令与中国行政当局关系之协定》在本次条约签订中具有积极意义。该协定不仅明确规定了苏军进入东北后与中国地方当局的关系，管理权限等，虽然斯大林不愿意在该协定中注明苏联军队应在日本战败后3个月内撤出中国东北，但斯大林保证在苏联军队应当在3个星期内开始撤离，其撤退时限最多不超过3个月。<br />
<br />
而《中苏友好互助同盟条约》则是苏联同中华人民共和国签订的一个新型的双边关系条约，不仅是一个军事同盟，更是一个政治同盟。该同盟不仅为共同防止日本帝国主义的东山再起，以及日本或其他用任何形式在侵略行为上与之相勾结的国家对苏联和中华人民共和国重新侵略，更是为加强中华人民共和国与苏联的友好与合作，符合两国人民的根本利益。双方愿以忠诚合作的精神，参加所有确保世界和平与安全为目的国际活动，并为此目的迅速实现充分贡献自己的力量。特别是条约的第四款，明确规定了中苏双方根据巩固和平与普遍安全的利益，对有关中苏两国共同利益的一切重大问题，均进行彼此协商。鉴于第二次世界大战已经结束，远东地区的形势发生了根本变化，日本帝国主义遭受到了失败，反动的国民党政府已经被推翻，推行与苏联友好合作的政策的中华人民共和国政府业已成立，双方决定重新处理两国间事务。在新条约中，旅顺、大连、及中长铁路问题已不作为苏联为取得某些特殊利益的前提条件，相反苏联方面顺应国际形势的变化，保证不迟于1952年末无偿将中长铁路的一切权力及属于该铁路的全部财产转交中华人民共和国。同时，双方同意在对日和约签订后，但不迟于1952年末，苏联军队即自共同使用的旅顺口海军基地撤出，并将该地区的设备移交中华人民共和国政府。与《中苏友好同盟条约》相比，在本次条约的签订中，中方完全彻底地收回了旅大的主权，保证了苏联军队（俄国）自1896年《 中俄密约》 签订以来由中国领土的永远撤出。<br />
<br />
通过以上对两条约签订的背景、谈判内容及条约文本的量化比较，我们就不难对两条约的性质有一个更明确的认识。《中苏友好同盟条约》是在中苏关系动荡不安的大背景下签订的，而且有损中国主权《雅尔塔协定》几乎完全限定了条约所规定的各项内容。在条约的签订过程中，中国国民党政府不惜以外蒙古独立为代价，换得苏联出兵东北，打败日本军国主义的侵略，结束坚持八年的抗日战争。斯大林从苏联在远东的战略利益出发，向中方提出一系列损害中国主权的苛刻条件，而希望苏联早日出兵东北的国民党政府几乎完全满足了苏联方面提出的所有要求和条件。如果说苏方在条约签订的谈判中还有些让步的话，那也是在损害中国主权和领土完整前提下的让步。然而《中苏友好同盟互助条约》的签订是在远东地区形势发生深刻变化，中国共产党人夺取全国政权后，与苏联签订的第一个双边关系条约。该条约充分反映出中苏之间在未来的新型合作关系，所强调的不仅是同盟，更是互助与合作。<br />
<br />
从两次签约的谈判过程和内容上看，前者突出的是苏联、斯大林为确保苏联利益而不惜损害他人的利益，甚至损害他国的主权与领土完整。但后者则截然不同，所体现的完全是平等协商与对话的原则。尽管《雅尔塔协定》的内容在中苏12月16日的会谈中被斯大林再次提及，但斯大林此时完全改变了像对待国民党谈判代表那样的态度，与毛泽东不仅充分协商，而且道出原委，最后并非在中方的压力下，准确地说，应当是在彼此交换意见的过程中改变了计划不修改1945年条约的初衷。关于后者仍有两个问题是当代史学界关注的焦点，一是条约规定苏联应在1952年末前撤兵，但直到1954年赫鲁晓夫访华后苏联军队才撤离旅大；再则是中苏双方签订的《秘密协定》。以笔者之见，苏联军队在1954年撤出旅大绝非是赫鲁晓夫送给毛泽东的“见面礼”，因为此时危及中国国家安全的朝鲜战争已经结束，苏联和日本已经签订和约，苏联之所以没有按条约规定的期限撤出旅大，是在中方的请求之下做出的决定。[34]至于秘密协定的目的是为防止第三方势力在东北以及新疆对两国安全的影响。在两大阵营对峙的情况下，中苏双方做出如此选择，不仅符合苏联的利益，更符合成立不久的中华人民共和国的利益。正是有了这个协定，才使中国共产党人得以在未来的时间里有足够的可能和措施保证自己边疆地区的稳定与安全。因此，凭借上述两点否定《中苏友好同盟互助条约》的平等性质，显然有悖客观历史事实。<br />
