																											<div id="postmessage_818671" class="t_msgfont">“内部毛选”：毛准备死四亿中国人来打第三次世界大战<br />
<br />
1973年左右，我在我们大学的内部图书馆（所谓“内部”图书馆是供院级领导和马列主义教员专门使用的）读到一份中共内部文献，名字大概叫《毛泽东选集/内部》，由中共中央毛泽东选集编辑委员会选编，党内限级阅读。该文献是中华人民共和国成立后至六零年左右，毛在中央高层的历次重要讲话，出版日期应该是在四清运动开始前后。书的厚度比公开发行的毛选四卷的一卷本略薄。<br />
<br />
该文献中的许多讲话过去都略有所闻，没有引起我太多兴趣。令我十分震惊的，是毛关于第三次世界大战的几次讲话。<br />
<br />
毛关于第三次世界大战的这几次讲话均集中在1955年，其时代背景是抗美援朝“胜利”后、赫鲁晓夫公开批判斯大林之前。讲话可能有3－4次之多，概括起来其主要内容是：<br />
<br />
1）世界大战并不可怕。第一次世界大战打出来一个社会主义国家苏联，第二次世界大战打出来一个社会主义阵营，第三次世界大战如果爆发，就可以实现世界大同。<br />
<br />
2）第三次世界大战应该早打，大打，打核战争，在中国打。<br />
<br />
3）第三次世界大战如果爆发，我建议苏联假装坐观，由我来带领中国人民把美国军队吸引到中国战场，我们同美国军队打常规战。战争扩大滚雪球，然后我们假装败退，逐步把美国军队引入中国内地，使美国军队陷入人民战争的汪洋大海中，从而迫使美国向中国战场投入主力军队。当美国将主力军队投入中国战场后，请苏联向中国战场突然投射原子弹，将美国主力军队一举歼灭在中国的战场上。<br />
<br />
4）这样的一场世界大战，中国可能会死掉四亿人口。但是中国用三分之二人口的牺牲换来一个大同的世界还是值得的。（当时中国的统计人口是六亿）<br />
<br />
5）死掉四亿人，还剩两亿人，用不了多少年，中国就又可以恢复到六亿人口了。<br />
<br />
毛讲这些话时中国还没有原子弹，只能借助苏联的原子弹投向中国的土地来实现。<br />
<br />
中国人预计死四亿，那全世界要死多少亿呢？相比之下，二次世界大战全世界共死了三千多万人，而且二次世界大战死那么多人未见得是战争狂人在战争前的预计和初衷。<br />
<br />
到海外的这些年来，我偶尔看到有“三亿”这个数字。最近看到马建介绍张戎的新书《毛，鲜为人知的故事》，知道“三亿”这个数字来源于苏共档案。这样看来，毛在不同时期，不同场合，在不同背景下对第三次世界大战有过多次不同的论述。<br />
<br />
据马建介绍，张戎的“毛传”提到，毛在苏联说过：为了世界革命的胜利，我们中国准备死三亿人。可见国外所传“三亿”这个数字是从苏联流传出来的。毛访问苏联是在1949年12月，当时中国人口的统计数字是4点5亿，三亿也正好是三分之二。如果今天有人准备牺牲三分之二中国民众，那就是九亿人。<br />
<br />
