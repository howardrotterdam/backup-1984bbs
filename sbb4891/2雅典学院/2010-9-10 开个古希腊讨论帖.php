													<h2>开个古希腊讨论帖</h2>
						
						
																											<div id="postmessage_824029" class="t_msgfont">第一次些类似费劲的东西，起由是应付老师的作业，但是既然写了，自己也还是满意的，就拿出来请大家来看看，大家看之余就给点看法，及以后这类东西该怎么写。也请大家尽量添加内容也当普及我们那缺掉的一步。因为，是命题要围绕古建筑的，所以就写建筑多一点，只把它当个由头，大家多出新观点，权当我抛砖引玉了。对了，这和本版块就是一个意思，很巧合。阿门<br />
<br />
&nbsp; &nbsp;写这篇小文有个缘由，前段时间因为选修课的机缘接触到了希腊文化的一些方面，进而延伸找了些其各方面的文献来读，乍读之下，我不能不在古代希腊的高度文明面前震惊不已，它的繁荣的经济，它的高度的民主政治，它的自然哲学，它的灿烂的艺术文化……不仅在古代世界的历史上是罕见的，也截然不同于我国的古代历史。这一切究竟是什么使其然的呢？所以我在闲暇时间写下这点在其建筑艺术和发展方面的所思所想，意在朋友们在这个基础上可以有更多的延伸，可以有一些想法，并向我们的顾准先生遥寄个人的敬意。<br />
古希腊的文明的肇始地是在爱琴海上的克里特岛。从公元前3000年到大约前1400年时其初起到兴盛的时期，创造了异常发达的文明，雅典王子忒修斯受公主线团如迷宫杀死牛怪的故事就是发生在这里，爱琴海更因此得名，这里曾被当成神话来看待，直到1900年克诺所斯宫被发现。这个近4000年前的建筑群是那么华丽舒适，竟然有暖气和冲水厕所，这可是数千年后的欧洲权贵如路易十四和伊丽莎白一世都没有享受过的，并且其大量的壁画对动物活泼的生命力和动态有准确的把握，精美绝伦，有极高的艺术价值。根据其文化遗迹的特征有受近邻希腊启蒙的特征，如它流线型的可以看出明显受到古埃及纸莎草式柱头的影响，当然作为一个岛国，因为其地域特征自然而然发展出了有异于埃及大陆性特征和阴森冥国的特质，做出了充满明亮实用的生活气息和注重生活欢愉的个性发展。就如当年在广袤的中华大陆上秦王朝奠定农耕文明大国的正式基础，在我看来西方社会一切的根基与制度在那时已经注定，不同的只是过程的波动变幻。<br />
&nbsp; &nbsp;&nbsp;&nbsp;之后希腊半岛经过迈锡尼人及多利安人的摸索走向了城邦制，这当然有各方面的原因，房龙有本书专门说到这个问题，而顾准先生则更多通过民族气质和其他方面的偶然因素进行了论述，比如贵族为了得到人民的支持常常对平民作出让步，限制特权阶级。希腊人的精神生活非常丰富，三权分立的政治理念就在古希腊产生，苏格拉底，柏拉图，亚里士多德，亚历山大。。竟然是师徒一脉相承，这也太集中了点。。如梭伦在公元前594年建立了平民广泛参与的公民大会，为雅典的民主制度奠定了基础，在‘伯尼克的黄金时代’公民大会为政府在外交，军事，财政等一切领域的活动作出决定，一个伟大的雅典被创立了。可见那时，现代西方国家的民主制度已经初具雏形，反过来讲，如果没有当年雅典城邦的创造性的尝试，如今为我们熟悉的民主政体变成了无源之水了。这和我国的发展轨迹很有不同，也说明了两个地域上的人民完全不同的民族气质。<br />
雅典得名于城市的保护神雅典娜，相传雅典娜和海神波塞冬{就是希腊电影里拿叉子的那位}争着做这个城市的保护神，而市民们选择了许诺赠与他们智慧的雅典娜。这个故事的寓意在现实中得以兑现，古希腊人热爱知识，重视教育，人才辈出，无数如雷贯耳的名字和这个城市溶为了一体。<br />
我国的部分精英分子一直来都有避世的做法，而且千年来都有传承，道教由此而立，而古希腊人不是如此，他们采取世俗的积极地人生观，他们认为生存是朝气蓬勃的赏心乐事，单纯充实的行动本身就是生存的目的。当然，他们认为自己是万灵之首，理应是万物的主宰，这也体现在他们和神的关系上。他们认为，神同样有七情六欲，也会嫉妒，也会犯错，在一定程度上具有人性，当然他们还有很多人因为品德高尚而深具神性，受到神的尊重，还有很多伴生在两界自由游荡，所以他们和神的关系实际上是一种类似平等交换的关系，献祭的目的是得到神的回报，这一点和其与统治阶级的关系接近，再者我想到战国时期我国也曾有过这样的记载，一个诸侯王以要求的是否实现来衡量献祭的祭品，这是个活泼的年代，可惜后来再也没有看到过。我想如果不去领会古希腊人的精神和气质的话，我们是绝对无法理解其建筑和美术的精义所在的。<br />
古希腊人的生活简朴，没有大建豪宅美邸的习惯，但是在形而上的事变活动之外，客观水平上他们为自己的纯净的美感和健康的创造力找到了一些宣泄的途径，恰好在其人生人物中有一群挑剔刁蛮的神灵需要去安抚和取悦。于是，他们将饱满的创造力和灵感花在你呢体现高贵的单纯和静穆的伟大的宗教建筑建设中，其中最主要的就是神殿和神庙---奉献给神明的地上居所和祭坛。<br />
神庙的建造在古希腊人看来可能是一个有趣的事，一次生命力的愉悦释放，在这个过程中诞生了经典的美学符号，三大柱式―陶立克式，爱奥尼亚式，科林斯式。这三大柱式。其实三种柱式有很大的传承关系，陶立克柱式没有柱基，柱头或柱首形式简单没有任何装饰，园主表面上有20条凹槽，彼此相交为锋利的棱线，在阳光下有明暗起伏转换的峻峭光影效果，大气而粗犷，有象征男人的意识，爱奥尼亚柱式则有所加以柔化的趋势，柱身的比例变得修长，棱线也变多了，并且棱线通常已经被磨为弧面，柱头上装饰涡卷，象征着女性的长发，柔和而活泼，最后的科林斯柱式这体现的一种文化演进的普遍法则，如果把陶立克式类比为盛唐时期的雄健激昂的古体诗的话，爱奥尼亚柱式则可以说是晚唐工整的诗歌，而更加华丽纤细繁复多样的科林斯式柱式则是那李后主的哀怨婉转了，这几乎在各个领域都是个神秘的魔咒，无可逃避，我国的古建筑也有这样的轮回不断出现。 <br />
 遥想古代雅典人，那样爱他们的雅典娜女神，为回报女神对他们的好意，回报那神给予他们最高贵的智慧，他们用最伟大的工匠，最杰出的雕刻家，最伟大的建筑师，为她修建了规模空前的帕提农神庙，让雅典娜成为了最享尊荣的大神，直面爱琴海的海风，遥想那遥远的时空，希腊人生活在物产贫瘠三面临海的希腊半岛之上，岛上最多的就是一样东西-大理石。洁白的大理石创造了美学的经典案例，诠释简洁与美的终极定义。贫瘠的土地狭小的空间让希腊人成不了农耕民族，炎热的天气让希腊人穿不了厚重的衣物，大片的荒漠让希腊人得不到过多的树木与生产不了大量的食物，但是也许一个民族真的有其宿命，空间狭小，由此希腊人成为了海洋民族，希腊由此永远是个开放自由的地方，种子是这样种下的，吃上了海产希腊人是不是变得更聪明也是个值得探讨的问题。<br />
&nbsp; &nbsp; 倒想起了【碧海蓝天】里的那个希腊，希腊人是海的孩子，海的朋友，他妈真正用心去追求这世界上最有意义的东西，那就是他们的生命本身，这是我很感动的一部电影。前段时间主流媒体说道，希腊的高福利导致的政府高财政赤字，为解决这个问题而造成的全国不稳定，这个时间被当做西方社会制度有很大问题的说辞，反衬我伟大制度的教材，这个事件对我国的官员来说确实不是一个事儿，太小了，太好解决了，一手把工资降下来得了，政府养了你这么多年，现在政府有危难，你小子费个毛话，赶快服从集体利益，这希腊鬼子就是贱。。没见我们在地震时强大的调动能力？不过这里面体现的是什么，这我们只有自己告诉自己了。关于天气炎热对希腊人的影响我认为是个客观条件，如果没有这个条件当然没有这样的文化，但是我觉得还是民族气质的影响为重，绝不见阿拉伯人是什么样的么？就是因为对自身的嫉妒自信，对生活美好的执著让希腊人喜欢上了人体美。其实现在我们看到的大卫像，断臂维纳斯等都是罗马人的作品，而罗马人几乎可以说是希腊人的学生，米隆德掷铁饼者我们现在只能从罗马人的复制品处一窥真颜，希腊人的成就可以想象，看着那美妙的人体曲线，我们只有感叹，美，你就在这里，哪位下凡的神竟用顽劣的石头如此具体化了你，这是如此不可思议！这和中国的写意不同，我也不敢分高下，这个年代也不好整理，对比汉代的一些雕塑的话（比如各种陵寝神道圣兽）。我还是比较喜欢希腊雕塑的。他有逼人心魄的魅力。&nbsp; &nbsp;<br />
 待续。。期待大家的充实。。<br />
