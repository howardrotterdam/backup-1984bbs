但这也不是什么好事，因为当贫穷的公民愿意为了福利而出卖权利的时候，民主制度的灭亡也就指日可待了。<br />
罗马帝国的衰亡，当然有各种原因，但阻止罗马人恢复旧日光荣的，并不仅仅是客观条件，还有主观意识：基督教对人类的摧残。当人们放弃了在尘世中追求自由和荣耀，而把眼睛盯住天国以后，还有什么可指望的呢？<br />
