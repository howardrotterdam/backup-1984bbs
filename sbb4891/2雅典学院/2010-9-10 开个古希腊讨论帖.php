																											<div id="postmessage_842771" class="t_msgfont">关注古希腊，我们就是要注重那种生机勃勃的异教精神。这种精神，文艺复兴以后，很长一段时间内是统治了思想界的，所谓“言必称希腊”，这对于现代文明的兴起至关重要。<br />
但是，随着现代民主国家的兴旺和成熟，一种暮气正在渐渐升起。现代文明，一如罗马文明一样，是古希腊人的学生。那么好吧，我们看看古罗马是怎么衰落的：<br />
