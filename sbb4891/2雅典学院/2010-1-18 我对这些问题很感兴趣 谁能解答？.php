													<h2>茅于轼:假如土地允许自由买卖</h2>
						
						
																											<div id="postmessage_442454" class="t_msgfont"><a href="http://blog.sina.com.cn/s/blog_49a3971d0100cbj4.html" target="_blank">http://blog.sina.com.cn/s/blog_49a3971d0100cbj4.html</a><br />
1993年11月23至28日我应亚洲开发银行之邀请，去马尼拉参加发展经济学第二届讨论年会。他们召开讨论会的目的有二，一是促进发展经济学的研究；二是让亚行的专业职员了解发展经济的最新进展，以有利于亚行的日常业务活动。<br />
<br />
&nbsp; &nbsp; 中国的改革给发展经济学带来重大的冲击。过去这方面的专家认为要是经济发展，必须有资金的积累，有对科技的重视；然而把中国的过去和现在比较，并不是在上述几个方面有了什么变化才使经济起飞的。中国的成功，从根本上看是制度发生了变化，使大多数个人的积极性被调动起来，引进了市场机制，纠正了价格的扭曲，使资源配置的效率得到提高。中国人以自己亲身的体验说明了市场机制是启动经济发展的动力。这些影响在去年的亚行发起的讨论会上得到较明显的反映。<br />
<br />
&nbsp; &nbsp; 十分有趣的是在会议之后，在亚行工作的我的一位朋友汤敏博士驾车陪我去看了马尼拉冒烟的山（Smoking Mountain）。这是一个在马尼拉郊区用几十吨垃圾堆成的山，方圆几公里。在山上居住着十几万贫民。菲律宾地处热带，几百万吨垃圾在烈日照耀下，熏发出恶臭。热量的积聚使拉及自燃，许多地方冒出一柱柱黑烟。肮脏、恶臭加上高温，使这块地方成了人间地狱。普通人连一分钟也不愿在此停留，但马尼拉有十几万人却终年居住在这座山上。我们驾车过时看到人们住在用垃圾里的纸板、塑料布搭成的小棚里，有的人追逐新装运到的垃圾车，寻找可以换钱的被富人抛弃的金属罐、废纸、塑料和玻璃碎片。这块地已在全世界闻名，被称之为“冒烟的山”。<br />
<br />
&nbsp; &nbsp; 作为一名经济学家，面对这样一幅图景，我不能不长时间思考，是什么原因，导致了如此严重的社会不公？马尼拉又有许多宫殿般的高级住宅。有一位普通的富人，他家的客厅一次可以容纳600人的宴会。菲律宾是美国培养的民主橱窗，可是“民主政治”并未解决如此严重的社会问题。相反，贫富悬殊已成为菲律宾的癌症。消费的畸形、人力资源的浪费、因为社会不公而引起的治安问题，使经济发展停滞，并进一步形成了政治上的不安。菲律宾的现实，对我国有什么借鉴之处呢？<br />
<br />
&nbsp; &nbsp; 造成当前菲律宾困境的原因当然是非常复杂的。但有一项政策值得我们密切注意，即关于农村土地允许自由买卖的政策。最近两三年来我国农业经济学家有鉴于土地产权不够长期稳定，影响农民培植土地肥力，造成单产徘徊不前，因此，倾向于允许土地自由买卖。这种政策虽然有利于土地的利用，但很容易造成剧烈的贫富分化。一些经济困难的农户很可能将分得的土地出售，以解燃眉之急，结果成为无土地的农民。他们丧失了最根本的生产资料，许多人涌进大城市找工作。当经济不景气时，失去起码的收入，最后只好住到“冒烟的山”上去。<br />
<br />
&nbsp; &nbsp; 当前我国也有大量农民流入沿海城市做工，但他们在农村仍握有土地。万一在城市里失业，他们可以退回到农村务农。所以土地为农民提供了最基本的社会保险。这一政策使得我国的大城市里没有出现许多发展中国家都有的贫民窟。相反，在大城市里立足且能租房子居住下来的，都是农民中有较高收入的具有一定专长的各种专业户。这一事实说明了土地政策具有非常的重要性。从菲律宾的经验吸取一些营养，或许有助于制定一个考虑更周到的农村土地政策。</div>

							
							
							
							
