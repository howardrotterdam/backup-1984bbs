																											<div id="postmessage_490790" class="t_msgfont">曾节明：乘火车实名制是胡锦涛极权倒退的新图谋<br />
&nbsp; &nbsp;（《自由圣火》首发 转载请注明出处并保持完整）<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;文章摘要: 与江泽民的混日子不同，胡锦涛为防红色江山变色，不惜向斯大林模式倒退；随着最近中共团派势力在权斗中占据优势，胡锦涛明显加快了倒退步伐。这次试行乘火车实名制倒退举措以“为民”的幌子出台，煽动民粹、蒙蔽愚民，狡诈非常，可谓是机关算尽。<br />
&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp;作者 : 曾节明,<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;l表rg：2/21/2010<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;今年春节，中共当局在反“黄牛党”的高调声中，在全国试行火车票购票和乘车实名制。海内外许多华人，为中共的高调所迷惑，还以为春运实名制是胡温打击倒票的的“铁腕新政”，是“为民办事”，殊不知，中共胡温当局试行乘火车实名制的真正意图，根本不是打击什么“黄牛党”，而是要钳制国人的迁徙、旅行自由―― 胡、温等专制统治者之所以推行实名制，醉翁之意不在票，在乎拦截访民和异议者也。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;为什么说此次实名制试行，反“黄牛党”是虚？<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;因为这一措施反倒票的作用很有限――比如，“黄牛党”分子可以采取化整为零、雇人分散排队购票，然后在集中倒卖的方式倒票；而且，在“实名制”下，“黄牛党”成员照样可以与铁路系统内的线人勾结，采取捂票/针对性售票，或假退票等方式搞到大量车票… … <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;与中共当局高调宣扬的利民效果完全相反，实名制的试行，非但没有缓解购票难、乘车难的问题，反而造成购票、乘车更加麻烦、火车站更加拥挤、铁路运营更加低效…实名制横蛮剥夺了遗失或正在更换身份证的国民乘火车旅行的自由权利，并迫使其他持证购票的旅客，不得不象乘国际航班那样、提前两个小时进站加入长龙队伍，耐心恭候几如海关检查一样的人票核对、身份查验，这就导致火车站比以前更为拥挤，旅客在购票排队的痛苦上，又得多忍受了一层新的进站验身痛苦…<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;实名制在大大增加老百姓麻烦和痛苦的同时，也极大地增加铁路系统运营的成本。为了配合实名制，诸多车次的列车进站时间必须比原先大为提前，原有的全国铁路调度时间表被打乱；为了查验身份证、进站验身，必须增加一大批人手和电脑等器材；因为火车站等候人群猛增，各火车站也需要增加一大批人员和设施以维持秩序…据了解，为了这次试行实名制，广铁集团、成都铁路局两个试点区的人员和设备成本增加了1亿元（《联合早报》）；因为实名制，几十万铁路部门的干部和职工工作量倍增，忙得焦头烂额，他们与乘客一样苦不堪言。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;可见，乘火车实名制不仅不符合老百姓的利益，也完全不符合铁路部门的利益，若无上级压力，中国的铁路部门决不愿做这样吃力不讨好的陪大钱买卖，因此可以推知：乘火车实名制的试行，不是铁道部的主意，而是中共中央和国务院的要求――乘火车实名制，就是胡锦涛的本意。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;那么，实名制岂不成了漫无目的的胡折腾？对老百姓和铁道部是这样，但对中共中央来说却非漫无目的之举。“俗话说“无利不早起”，胡锦涛、温家宝等技术官僚出身的最高当权者虽然鄙劣，但并不弱智，他们决不会看不到实名制的无用和劳民伤财，但他们就是要推行，因为实名制虽然对老百姓和铁路部门都没有好处，但是有利于维护胡、温等最高统治者的专制权力（或曰：有利于“大局稳定”）<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;其实，隐蔽于“实名制”中的最大功用，是能够监控国民的迁徙和旅行：随着“实名制”的试行，电脑身份证查验互联系统配备于各火车站，它可以方便地与公安网连接，供人轻松查阅购票者的户籍信息（如“犯罪纪录”）；凭借联网查验系统，中共当局可以借口“安全”需要、迅设置铁路旅行的黑名单，一如设置出入境黑名单一样，凭借这样的“实名制”，中共当局可以非常有效地从源头阻止各地访民、维权、异议人士聚集北京。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;目前中共当局对进京访民群体的打压，主要仍然停留在如消防队灭火的被动防御状态――不断增加军警，于北京之内与访民群体作无休止搏斗，但对于访民进京，仍然无可奈何，因此近年来访民越打越多，防不胜防…中共胡锦涛政权，显然要藉实名制之实行，把进京访民封堵于其户籍地，以彻底扭转现今截防、“维稳”的被动局面，在北京真正实现“和谐社会”。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;另外，近年来鲁德成、余志坚、喻东岳、耿和、张青等一批受监控的知名异议人士极其家属成功逃出中国，造成国际舆论被动，这也令中共最高统治者感到有必要收紧铁路旅行控制，以强化对异议人士的人身限制。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;应该说，乘火车实名制，非常符合胡锦涛一以贯之的“防微杜渐”极权倒退统治思路。一直对朝鲜的“稳定”神而往之胡锦涛，做梦都想在中国实现朝鲜式的“和谐社会”，明眼人也不难看出：乘火车实名制与朝鲜外地赴平壤的“护照”制度深具异曲同工之妙。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;由此可以推断：一旦乘火车实名制在全国顺利推广，接下来中共当局必然会实行长途巴士乘车实名制、租房实名制、购买手机、打印机、传真机…实名制。不要以为这是危言耸听，胡锦涛上台近八年来，“温水煮青蛙”式的极权倒退一直在不声不响的进行着，这么些年从中国大陆走过了的人，可以回头掂估一下：从出版影视到互联网到经商贸易…八年前的那点有限自由如今萎缩了多少？<br />
&nbsp; &nbsp;<br />
