													<h2>关于毛泽东的智力</h2>
						
						
																											<div id="postmessage_749060" class="t_msgfont">我之所以想到这个问题是因为最近常见有这样一种议论，说毛泽东虽然如何如何，但他是百千年一遇的奇才，我也在类似猫扑一类的网站上看见许多毛的崇拜者，也见许多出租车司机或者生意人供着毛泽东的像，我估计大约也是这个理由。我就想：要破除对毛泽东的崇拜，如果不能从智力上把他还原成一个人，大概不会有什么效果。<br />
&nbsp; &nbsp; 在讲毛泽东的智力之前，我先讲两件自己经历的事情。一件发生在数年前，我工作单位的政府主管部门来考察候选干部，组织部的几个人一个一个地约谈，让你讲讲对其他候选人的看法。我总是力图客观，事实上总还是以肯定为主，但我后来知道另一些人是大力讲了我的坏话的，譬如自由散漫、不负责任等等。我自然落选了，讲我不好的人升上去了。后来有人说我呆，好听点叫“书生气”，我却想我这样做是求自己心安。另一件发生在不久前，我与一个人狠狠地吵了一架，起因是工作原因，但让我觉得他的人品很差，所以我也讲了一些狠话。次日见到他我也不打招呼，没想到他却若无其事，又与我很热情地聊起无干琐事，说实话，我都觉得有点尴尬。他原是我的部下，现在也升上去了。请大家记住，在这两件事里，这两个主角的智力我再谦虚地说也并不比我高，但他们有一点比我强，就是所受道德约束比我要小。<br />
&nbsp; &nbsp; 下面我来讲讲毛泽东的智力，由于我只是一时兴起展开这个话题，也懒得去查找相关的资料，所以只是凭记忆大概说说，我不希望大家在细节上纠缠，除非你有很实在的依据。我想随便讲讲关于证明毛泽东智力问题的几件大事。<br />
&nbsp; &nbsp; 第一、关于游击战，这一直被吹成毛泽东的指挥智慧，但实际上游击战的思想，包括十六字方针是朱德提出的，这一点在剑桥中华人民共和国史中提到过，在数月前的《炎黄春秋》中也有一篇文章提到。但我估计毛泽东在执行这一点上可能更有过人之处，因为游击战说到底是一种痞子打法，在抗战时期，面对日本人的侵略，这种战法在道义上就有点说不过去，所以朱德、彭德怀、林彪等人对毛都有意见。<br />
&nbsp; &nbsp; 第二、关于长征。后来的党史把反围剿的胜利都说成是贯彻毛的思想，失败是因为驱逐了毛。随着有关资料的解密，这也是不实之辞。毛泽东当时其实并没有最高军事指挥权，而且如上点所说，游击战实际上是朱德的指挥思想。这一点在上点提到的《炎黄春秋》那篇文章里也有详细记载。朱德是个厚道人，对后来党史的谎言基本上抱着一种无争的态度。长征途中，毛泽东出于私心造成一些重大损失，对此当时也曾遭到很多人非议，譬如林彪，就上书要他把军事指挥权交给彭德怀。<br />
&nbsp; &nbsp; 第三、关于持久战。持久战的提出者是白崇禧，而蒋介石也一直持此想法，他与汪精卫分道扬镳，提出“攘外必先安内”、以空间换时间实际上都是这一思想的具体贯彻。而且持久战这一战略思想的具体实施者，也是蒋而不是毛。关于抗战，我还要说一点。我们现在说起毛泽东的功过，总不免与斯大林作对比，在我看来，卫国战争的胜利倒实实在在是斯大林的历史功绩，抗战胜利是毛么？他孙子说：是我爷爷。庄子说：“窃国者侯。”<br />
&nbsp; &nbsp; 第四、关于西安事变。毛泽东当时是坚决主张杀蒋的，后来的党史掩盖了这段历史。在我看来，这当然是一个很愚蠢的想法，因为杀蒋以后，国民党分裂，各路诸侯纷争重起，毛将无立足之地。这有点类似当年刘备不杀曹操的道理。后来是因为斯大林力主不杀蒋，才促成国共二次合作。<br />
&nbsp; &nbsp; 第五、毛泽东走上神坛。这与一帮中共元老关系很大，例如刘少奇，七大上提出毛泽东思想，是有一些私下交易的。后来七千人大会后与毛闹翻，他又说，当年我提出毛泽东思想，现在也可以不提。俗话说：“请神容易送神难。”刘少奇智商也不咋地。<br />
&nbsp; &nbsp; 第六、建国后。在这一段时期，关于毛泽东的智力我就不想多说了。在国际关系和国内政策上，他做过什么具有智慧的判断？他以为自己德高望重，没想到双百挨骂，就受不了；他相信麻雀是四害，敲锣打鼓上天安门；他相信亩产几万、几十万，说粮食多了吃不了；他相信他老婆，结果捧出一个历史上最大的丑角；等等等等。还有一句话：上了神坛，再愚蠢都是神。<br />
&nbsp; &nbsp; 那么，毛泽东竟没有过人之处么？肯定不是，否则，他也上不了这一个神坛。那么他的过人之处到底是什么？肯定无关乎智力，照我看就是如我在前面提到的，他没有道德感。张国焘对此就感触很深，他在回忆录里提到一个细节，我印象倒很深。张失势到延安后，有一次干部子弟学校组织演出，毛点名让张的儿子演叛徒，然后在台下很得意的说：这小子演叛徒还真象。张国焘毫不客气地说，毛是个小人。然而，即使在这一点上毛也未必是多少年一遇，这种毫无道德感的极端小人其实在任何时候都不少，但是君子能群，小人相残，最后只是一个小人能够胜出，结合着结果来看，毛倒还真是百千年一遇。<br />
&nbsp; &nbsp; 问题是，如果我们今天还在崇拜毛泽东，那就实在与文明的发展方向相悖了。</div>

							
							
							
							
