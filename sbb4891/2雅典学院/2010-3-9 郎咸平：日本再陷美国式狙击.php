													<h2>郎咸平：日本再陷美国式狙击</h2>
						
						
																											<div id="postmessage_508939" class="t_msgfont"><a href="http://blog.qq.com/qzone/622004678/1268041005.htm" target="_blank">http://blog.qq.com/qzone/622004678/1268041005.htm</a><br />
<br />
　　20多年前，一纸广场协议，日本经济陷入了20年的衰退，至今没有恢复元气，而广场协议的主导者美国，借助金融工具和高科技，成功拉开步步紧逼的日本数个身位。25年后，日本最大的汽车制造商、也是成功超越通用成为全球第一大汽车制造商的丰田陷入召回门。据媒体统计，因踏板问题，丰田目前的召回数量已超过其去年718万辆的全球销量，为此将付出约135亿人民币的代价。<br />
　　丰田召回门事件是否是一场广场协议似的狙击战呢？丰田是否就一蹶不振？谁又会是受益者呢？<br />
　　丰田的确应该为它的产品质量问题付出代价，但本文的目的是探讨丰田召回事件背后真正的故事。<br />
　　大家在看热闹的时候，用脑袋想想这几个问题：丰田召回事件公众关注度为什么陡然上升？美国车召回真的比日本少吗？为什么美国媒体不穷追猛打福特呢？福特召回的次数和数量比丰田只多不少。<br />
　<br />
&nbsp;&nbsp;　潜伏<br />
<br />
　　带着上面3个问题，很多事件一深挖，道理就自然明白了。<br />
　　为什么美国媒体穷追猛打丰田呢？因为Dimitrios P. Biller这个丰田美国的前雇员，源源不断地向媒体提供独家猛料。这个人成了福特扭转时局的关键。而这个局，前面是一场悄无声息的“潜伏战”，后面又是一场招招毙命的“狙击战”。<br />
　　潜伏战的第一步，物色人物。比勒他在高等法院帮福特汽车辩护了几个大案，于是很快得到福特汽车赏识，邀请他参加“下一代诉讼律师训练计划”，而这是福特的“黄埔军校”，每年在全美国范围内只物色大概16名青年律师。<br />
　　第二步，全面培养。福特专门给他安排了三个月时间在福特全球总部的法务部，专门研究汽车相关的法律乃至工程的各种问题。而他把1995年到1997年福特所有的官司都研究了一遍，告诉福特在集团诉讼里什么特征的原告你容易挑出毛病，什么样的你最好要避开。<br />
　　第三步，准备潜伏。福特更是老谋深算，这么聪明的律师，福特竟然最后不给他留在自己的法务部里，而是培养好了之后呢，让他回到自己的律师所，遇到案子时才委托给他。所以丰田看到的是什么呢？这个人帮福特打官司是一打一个准，而且对各种各样的汽车事故啊都了解得不能再清楚了。所以，2003年礼聘比勒律师加盟丰田。<br />
　　第四步，打入内部，窃取机密。表面上来看，他算是相当尽职尽责，三年里代理丰田应战12官司，每战必胜。实际上呢？比勒后来在其公开说明中承认，“在为丰田时，便着手开始搜集，发现300起翻车事故的相关数据，在获得丰田美国公司认可的情况下已被销毁”。<br />
　　而他掌握到了足够的证据，至少会让丰田过去十年里的官司可能都要翻案。可是，他却不急于借此要挟丰田，恰恰相反，他给丰田什么印象呢？他没什么核心机密，而且已经被我们丰田用370万美元的遣散费封口了。<br />
<br />
　　等待<br />
<br />
　　丰田却不知道这恰恰是狙击战的开始。成功的狙击讲求三个要素，第一是时机，第二还是时机，第三个还是时机。<br />
　　这第一个时机就是你要麻痹敌人，所以放松警惕，从而给你足够的时机观察和搜集他更多的弱点。所以，虽然比勒是2007年离开丰田，可是却在两年之后的2009年7月24日,才把75页厚的起诉书提交给了加利福尼亚中部地方法院。你想想看，出问题的汽车都是什么时候生产的？主要都是比勒在职和离职以后，为什么？因为比勒让丰田相信福特的质量也是马马虎虎的，而在美国这种马马虎虎是无所谓的，只要丰田手里有比勒这样优秀的律师。<br />
　　第二个时机就是对自己最有力的时机。你想想看为什么不在2008年狙击？因为那时福特重组尚未完成，现在呢？福特的两大美国竞争对手都在经济危机中受到重创，现在还拦路的就只有丰田了。所以，2009年，福特选择了一个最好的时机给丰田布下狙击圈。<br />
<br />
　　出击<br />
<br />
　　正面狙击。7月份，启动诉讼。诉讼本身不是目的，而是给丰田脑袋蒙住，让它无暇兼顾。<br />
　　催泪弹。8月份，本来丰田准备诉讼，可是福特却把重点转移到了媒体。8月发生雷克萨斯ES350撞车事故，加州公路巡逻队长一家无一生还。福特动员媒体公关，美国媒体铺天盖地都是这个新闻，在电视上不断滚动播出，让大家都觉得这位队长生前尽职尽责，而一家人无一生还，可见这个事故是多么惨烈。所以，这么尽职尽责的公路巡逻队长出事故，唯一的可能原因就是丰田车质量的问题。<br />
　　政府出面，背后冷枪。9月14日，加州法院要开始第一次听证，所以丰田全部精力都投在这个听证会上，对于加州撞车惨案，丰田还在麻痹之中，觉得靠律师就能摆平，所以拒绝道歉，拒绝承认自己的车有质量问题。可是福特却疏通美国调查部门，在背后开冷枪。美国高速公路交通安全管理总局出来了，公布的不是正式调查报告，而是初步调查报告，因为这么短的时间，也没法完成正式调查。初步调查的结论是：“事故发生当时地板垫卡死油门，不合理的油门设计可能为事故原因”。换句话，就是一个莫须有的罪名，目的就是要打压丰田。<br />
　　媒体扫射。马上就要进入10月份了，在9月30日，丰田以为只要低头认错就可以息事宁人了，所以宣布召回380万辆“脚垫门”汽车。可是还没等丰田忙过来这个问题，狙击点又转向了丰田车的其他问题，甚至从美国扫向日本，操作丰田在日本国内销售的&quot;普锐斯&quot;存在刹车失灵的问题。再把用同样零部件的本田汽车也扯进来，最夸张的时候，你看华尔街日报日本专栏十条里八条都是召回。<br />
　　专家出马。到了11月，丰田以为只要认错了就没事了，可是狙击点再次转移，美国ABC电视台在25日采访美国汽车安全专家，他们说丰田这个维修方案是治标不治本的，制动系统的安全隐患没有彻底消除。而与此同时呢，美国交通部连续发出三次新闻公告，严重关切丰田的问题，要知道，在11月当月这样的公告一共才有四个。<br />
　　12月份，丰田承受不住压力，又召回了380万辆车。很快美国人就开始放长假过圣诞节了，丰田以为没事了。可是就这个时候，美国已经把这件事情升级到贸易战的层面了，这样对丰田的狙击就不仅仅是福特的事情了，而是美国政府的事情了。<br />
　　这就是第三个时机，政府全面介入的时机。国会调查，不是为了调查而展开的调查。其实这就是丰田被美国折腾晕了的一个标志。我们中国去年应战美国的轮胎特保案也是这样。就是没搞懂是谁在搞我们。<br />
　　所以，你就明白了，他们为什么要调查你呢？不幸的是，丰田高管此次来国会山似乎仍没有摆正心态。他们没意识到他们是来被羞辱的，仍在为系列事故极力辩解。<br />
　　所以大家明白了吧！9个小时的听证会，完全是美国国会议员给你设计好的。丰田章男的出席长短和形成都是人家预设好的。这个预设好的议程就是先听受害者控诉，再听国会内的大佬定调，然后呢就请一个汽车专家来讲解技术，让大家觉得前面说的都是对的，当然了，心里的疑问还是非常多，这时候再请丰田章男同志上台接受大家的问询。换句话说，议员憋了半天的问题咆哮而出，而且越尖锐越有可能上晚间新闻的头版头条。人家就不是听你道歉的，开听证会就是为了损你的，所以人家故意准备了很多细致的工程问题，你还不能撒谎，所以唯一的答复就是“不知道”，这样美国老百姓看到的就是一个支支吾吾，啥也不知道的丰田章男。所以，中国轮胎特保案等到听证会开始了才准备应诉，这已经晚了，因为整个听证会的议程已经被设定好了就是让你当场毙命的。<br />
　　更重要的是什么？所有听证会上的话都可能被律师用来起诉丰田。丰田汽车至少面临五重诉讼潮。<br />
　　第一波诉讼潮是因为油门事故而死亡或者受伤引发的民事诉讼。仅死亡案件就有34起。所以这些案件的起诉 都将是个人伤害。<br />
　　第二重诉讼则是购买了丰田车但是没有发生任何事故的车主。这将是一个天文数字的索赔，因为涉及到的几款车型有800万辆的销售量。这些车主所得到的赔偿将主要是车价的损失。<br />
　　第三重诉讼，则是美国州一级的政府通过检察官提起的诉讼，主要是根据各州的消费者保护法，为购买丰田车的车主进行消费者保护。<br />
　　第四重诉讼，是美国联邦政府进行的刑事诉讼。美国联邦政府已经启动了对丰田质量门的刑事案件调查。 如果有相关情节发现，那么联邦政府将启动针对丰田相关人员的刑事诉讼。<br />
　　第五重诉讼，则是美国证监会针对丰田的上市公司启动的诉讼，如果美国 证监会发现任何丰田隐瞒问题，没有向投资者充分相关披露相关信息，或者误导性的公布了财务数据，那么证监会将向丰田提出民事诉讼。<br />
 <br />
　　我在《新帝国主义在中国》一书中，解读了帝国主义从来没有离开过中国，而是借尸还魂，其实，帝国主义之间也还有斗争与阴谋，并不是铁板一块。<br />
　　看了这些很深刻的内容，你是不是觉得大爽，觉得中肫车有机会了。我告诉你丰田事件，我们要冷静对待，我们的汽车还差得太远，如果中国汽车制造企业陷入丰田似的召回事件，你知道结果会是什么吗。而这些大企业背后的博弈，都不仅仅是企业层面，我们的铁矿石谈判，我们经历的轮胎特保案，无一不是如此。我们在了解丰田召回事件背后的博弈时，那是给中国企业提个醒，冷静之后，中国制造业仍然要学习日本的制造业精神，其中包括丰田制造。在日本，还有另外的企业家精神，那就是我在《财经郎眼》谈到过的日本经营四圣之一的稻盛和夫，他在其所著《活法（修订版）》一书中倡导的精神，虽然我并不看好他接手日航，但不排除我对这个人的尊重。<br />
　　我们的企业家不要只看到丰田今天所遇到的困难，而更要知道丰田为什么能强大到这个地步、要真正了解丰田及日本制造的精髓。丰田的核心、生产方式、思维方式以及组织管理都是值得中国的企业家学习的。《丰田DNA》、《丰田方式――真传：丰田自己说》、《丰田可视化管理方式》、《丰田现场管理方式》，《图解丰田生产方式：图例解说生产实务》等，关于丰田的书实在是太多了，我说过我们的民族是一个浮躁的民族、我们的很多企业家是不爱读书的。但是只要我点出来之后，就有人会买很多书，买来之后也不好好读，可能连一本也读不完就自我感觉良好了。我希望看到我文章的人，能静下心来好好读书，更要好好思考。</div>

							
							
							
							
