													<h2>国际特赦撰稿人谈新疆骚乱报告</h2>
						
						
																											<div id="postmessage_721002" class="t_msgfont">专访：国际特赦撰稿人谈新疆骚乱报告<br />
bbc中文<a href="http://www.bbc.co.uk/zhongwen/simp/china/2010/07/100702_iv_ai_xinjiang_report.shtml" target="_blank">http://www.bbc.co.uk/zhongwen/si ... njiang_report.shtml</a><br />
<br />
国际特赦呼吁中国政府就去年新疆的骚乱展开独立调查<br />
<br />
人权组织&quot;国际特赦&quot;周五（7月2日）发表报告，呼吁中国政府就去年新疆的民族骚乱事件展开独立调查。这份在&quot;7&#8231;5&quot;事件一周年之际发表的报告题为&quot;公正，公正--中国新疆2009年7月的抗议&quot;。<br />
<br />
报告发表前，BBC中文网记者乐安采访了报告撰稿人之一、国际特赦组织中国研究院弗朗西斯女士，她首先介绍了报告的主要结论：<br />
<br />
在准备报告的过程中我们采访了几十名“7・5”事件之后从中国逃到国外的维吾尔族人 ，他们都是“7・5”和后来几天事件的见证者。他们给我们讲述的情形让我们对中国官方有关事件真相的说法产生了深刻的怀疑，他们描述军警对和平示威者使用了暴力。<br />
<br />
去年7月5日下午，大批维族人组织起来到乌鲁木齐人民广场集会。我们的理解是，示威是和平方式开始的，但不少采访对象向我们描述了警察和安全部队如何过度使用暴力来制止示威。<br />
<br />
不过，我们不清楚在后来形势是如何转变的。我们知道那天晚上发生了暴力冲突，但我们很难判断到底是什么事情引发了暴力。不过，清楚的一点是，示威开始是和平的， 人们的不满也只是针对政府的。抗议者的怒气主要是和几周之前韶关发生的造成至少两名维族人死亡的骚乱有关，他们不满政府的不采取什么行动的做法，这种愤怒和长期积累的针对政府未能解决民族歧视问题的怨气交织在了一起。示威的目的就是要表达这种不满，不幸的是，对示威的镇压可能引发了后来的暴力冲突，这当然是令人遗憾的。<br />
<br />
问：中国当局说示威和暴力活动是海外维吾尔人团体组织策划的，在您的调查过程中有没有发现这方面的证据？<br />
<br />
答：我们知道这种说法，但我们没有看到任何海外“指使”的证据。我们都知道中国的互联网很活跃，在新疆、在乌鲁木齐也是这样，而且有不少维语网站。几乎所有我们采访的人都指出在事件发生前的几天，网上论坛和博客已经在讨论要组织点儿事情，很多初中生、高中生也参加了讨论，特别是关于韶关的事件，一些韶关事件的视频也在流传。显然维族社区当时确实在酝酿着什么。<br />
<br />
问：那他们有没有就上街抗议进行组织、计划、协调呢？<br />
<br />
答：很明显有人做了决定，计划当天下午去示威，但是什么人做的决定我们不知道。示威计划很多人都知道，而且因为广泛流传在网上，我想当局也知道这个计划，不是什么秘密。<br />
<br />
问：中国的一些网站和中国政府后来也公布了一些视频和图片，显示一些汉族死伤者，有人身上有严重的刀伤，您有没有了解到示威者携带和使用武器的情况？<br />
<br />
答：我们也看到了这些视频，包括后来官方电视台播放的视频。毫无疑问当天晚上发生了暴力， 毫无疑问视频中有人挥舞木棍和铁棒打人，不同民族的人受到了袭击，造成了死亡。但我们不知道暴力是如何引发的，这也正是需要调查的部分。我个人没有看到用刀刺人的视频， 我采访的人没有一个带着武器，包括棍棒石块之类去参加人民广场的示威，也没有对警察使用暴力。<br />
<a href="http://www.bbc.co.uk/emp/pop.html" target="_blank">http://www.bbc.co.uk/emp/pop.html</a><br />
<br />
问：过去的一年中国也改变了一些对新疆的政策，比如原来的党委书记被换成了比较开放的人物，您怎么看这些变化？<br />
<br />
答：现在还很难说，我们没有看到重大变化，我们希望中国能采取不同的做法。更换当地党的领导人可能因为那次事件本就是当地的一个失职。我们也把事件看成是一个中国政府政策的失败，中国当局一直在讲民族和谐，每个人也都希望民族和谐，但中国当局应当为没有真的实现这种和谐承担大部分责任。<br />
<br />
我接触的很多维族人都抱怨政府的政策，特别是经济、就业方面的歧视，对他们传统文化活动的取缔。当然这不能当作对无辜者使用暴力的借口， 我们国际特赦组织更不认同这种暴力。但中国政府显然没有采取足够的行动正视几十年来让当地人觉得不平等、不公正，产生积怨的政策。<br />
<br />
问：说到经济权利，前不久中国又接连推出新疆投资发展计划，您觉得经济发展本身能解决新疆的问题吗？<br />
<br />
答:：大笔投资本身我想是好事，但是其他问题不解决，光靠这些经济措施不会有助于妥善处理民族关系。比如就业歧视问题，我接触过几十名受过良好教育的维族人，从某种角度他们也是中国政府政策的“受益者”，他们能到东部地区去上学，能讲流利的普通话，但是在找工作的时候就是没人雇用他们，申请的职位最后都给了汉人。<br />
<br />
