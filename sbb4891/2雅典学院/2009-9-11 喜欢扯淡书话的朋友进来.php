													<h2>喜欢扯淡书话的朋友进来</h2>
						
						
																											<div id="postmessage_236773" class="t_msgfont">周五晚上，在家陪怀孕的老婆，不出去潇洒。<br />
<br />
老婆睡着了，我来写书话。<br />
<br />
在线写，如果1984的兄弟们这时候还没有集体消失，消失在勾女、招妓、耍鸭子的海洋里，不妨过5分钟刷新一次。<br />
<br />
今天去了趟复活的成都古籍书店。大约六、七年前该书店宣布破产，同名的出版社也倒闭。其库存被当地几个旧书商瓜分，数车载走。我知道其中一个叫郭眼镜的，堆了大约8万元的货。2007年，他将其中10多种明末、清中叶前的刻本出手，已然回炉12万。而这只占他所堆旧书的总市值的什一。<br />
<br />
我也曾当过几年的线装党，因此十分嫉妒郭眼镜等人。但现在我已经不大玩线状了，两个理由：1、经济压力（我只收不卖，05年后线装书的价钱是孙悟空打小跟头云与蜘蛛精性交――一日千里）2、玩物丧志。线装书中有营养的并不太多，一味沉溺于所谓版本与市价，难免染上艺风老人的商贾恶习气。<br />
<br />
今天去古籍书店，在成都购书中心3楼。该中心刚花了半年时间装修，环境不错，可能是西南地区购书环境最好的书城了。古籍书店的堂子也算亮堂，但所进新书全是市面上常见的，且不算全，有时还有极烂的书混进去，比如文白对照，经典快读一类的。至于线装书部分，实在寒酸，镇店之宝不过是道光间的一函30多册的笨重文集。还不如我家藏的康熙年间施注苏诗，一函10册。软体字写刻本，棉纸。字漂亮极了，纸晶莹如玉，墨色浓厚如猛将之髯。<br />
<br />
没什么可买的，我就顺便视察了下购书中心2楼的历史专柜。<br />
<br />
一去眼睛一亮，看见郭廷以《近代中国史纲》。此书我曾在《中国史入门读物20种推荐》中力荐，其时大陆只有1999年中国社科的印本，且早绝迹市面。今日重印，实在令人开心。<br />
<br />
重印经典好书，是对不良旧书商最严厉的打击。<br />
<br />
记得在任乃强的《华阳国志校注图补》再版前，1986年出版的刘琳校注的华阳国志被旧书商炒到150元以上。同样价格离谱的旧书还有很多，譬如丁文江、赵丰田编纂的《梁启超年谱长编》，今年也再版了，再版前该书的复印本都要卖100元，还有钱伯城笺校的袁宏道集（80年代上古版，全三册），今年再版前，旧书商居然一套卖到500元以上。如今这帮旧书商孙子只好大降，袁宏道集的旧书直接落到几十元，华阳国志的旧书也跌到了30元-50元。至于《梁启超年谱长编》，除了利欲熏心且脸皮极扎实的傻逼，没人敢再卖标价100元的复印本了。<br />
<br />
说回来，我直接奔向郭氏此书，开卷前，心头忽然颤了一下，不会又是删节本吧？我还记得徐中约的《中国近代史》，大陆引进后删掉了好几万字，读到80年之后真是鬼冒火。<br />
<br />
大陆出海外书籍爱删节，乃是老传统。萧公权的自传在大陆出版，惨被删节近百处，删节地方还标上方框，注明此处删去多少字。一下子就把人家好好的一本自传，搞成了贾平娃的《废都》。有些编者为了安慰读者，特别标明是“有极个别删节”，譬如大陆出的费正清编的《中国的思想与制度》，而我详查内文，麻痹的哪里是极个别，是整段整段地删节。编者的“极个别”，我可以找出另三个字来形成绝对，曰“一小撮”。<br />
<br />
还好，《近代中国史纲》的出版前言光明磊落地表示，作者有些观点我们不同意，但为保留原貌，未予删节。<br />
<br />
吻您！这本书的编者，出版人！您们用实际行动给中宣部部长的名字抬了个恶杠。丫的本名叫刘云删，什么意思呢？就是刘说，删！哈哈<br />
<br />
说回来，在中国近代史概述类或曰简史类著作中，郭氏此书的质量当居前列。我心中的四大件是陈恭禄《中国近代史》、李剑农《近百年中国政治史》、郭氏此作、徐中约《中国近代史》（排名按出版年月）。此外蒋廷黼还有本《中国近代史大纲》，但一是太简略，二是属于以论代史，故未列入。至于中共这条线的近代史家，范文澜、林增平、陈旭麓等，都有《中国近代史》同名著作（范只出了上册，陈氏著作名略有小异），其中陈旭麓的最值得读，余则无甚大可观之处。至于各大院校自编或者奉旨编纂的种种《中国近代史》，可以直接扔进动物园给猩猩擦屁股了。<br />
<br />
郭氏一生致力中国近代史，曾任台湾中央研究院近代史研究所长，所编《中华民国史事日志》全四巨册，极有用。此外，郭氏重视口述历史，组织人力对台湾及海外幸存的近现代史上的重要当事人采访，整理出书数十种，也颇具史料价值。而他主持近代史研究所时，还酝酿组织《近代中国史料丛刊》的出版，后来沈云龙经过20多年的艰辛努力，成三编近百辑，上千种史料得以汇编。此丛书也可视作中国近代史的“四库全书”，却又没有四库抽毁之恶习。而大陆1949年后陆续编成的近代史料丛刊八大件，部头分量远逊，却深得四库之编纂妙趣。<br />
<br />
说回来，郭廷以《近代中国史纲》的写法比较朴素，论文采与情感不及李剑农，论综合与分析不及徐中约，但其著材料扎实（且多第一手材料，徐中约则多是二手），信息量大，开初读起来可能枯燥，读进去之后始觉其精湛。最重要的是，郭氏始终保持一个客观史家的态度，其立场于国共双方均不讨好，亦不歧视。<br />
<br />
校订完此书的第三天，郭氏就病逝于美国。他晚年受人排挤而离台，在美国度过最后的时光，常颠沛于各城市之间，又乏知音共谈，只有始终相濡以沫的妻子李心颜，为他充当助手，也聆听他的凄苦心声。　　<br />
<br />
说回来，在书店，我拿着郭氏此书就奔向收银台。先问打折么？（我已经很久不去书城，一般常去几家熟悉的学术书店，都享受贵宾会员折扣）小姐愣了下，说可以办会员卡。我说，怎么办？她说，满68元就可办一张。我又问，办了后几折啊？她说，不打折，积分。积分够了可以兑换礼品和图书。<br />
<br />
当时我就跳了起来，麻痹的您们这儿是红旗超市吗？把我当家庭主妇或家庭太婆吗？还积分呢，换啥啊？买够1万元，换一瓶淘大酱油，欢天喜地抱回去？或者换一本郭敬明手抄小说选，或者麻痹的什么朝的那事儿全集？<br />
<br />
日你妈的批，老子不上这些当。我马上掏出川大一个书店的电话，严厉地问店员，进此书没？店员查了下，说，有。哈哈，我就得意洋洋地打车奔向该书店了。我算了笔账，这书66元，我去那书店打75折，省下16元5。打车从购书中心去那书店，差不多14元。买完书我走回家，整整省下2元5.关键还不在这2元5，在不该惯着这帮不打折的书霸们！<br />
<br />
那位无辜的收银小姐不知道，她遭遇的不是一般人，是资深的，眼力犀利，精明强干的，高中曾经获得省奥数二等奖而至今仍能用史丰收速算法秒杀任何一种账单无论是买书买火锅单冷淡杯单还是红珊瑚夜总会单的宣纸算盘。（为什么不是铁算盘而是宣纸算盘呢？因为人家要装一装文化逼嘛，哈哈）<br />
<br />
到了川大的书店，发现他们将郭氏此书放在最不显眼的地方，立即语重心长地批评了他们，并预言此书将大卖，希望尽快提到亮堂处来。之后，一高兴又四处打望，买下如下几种<br />
<br />
任半塘《唐声诗》（全二册）、《唐戏弄》（全二册）、仓修良《史家・史籍・史学》（一厚册，仓氏没什么才气，但有点儿力气，特别是对章学诚颇下了些功夫）、邓之诚《东京梦华录注》（《东京梦华录》我已经有两个版本，但邓的版本要收。他的《骨董琐记全编》，特别适合睡前翻看耍，而《中华二千年史》，则是相当不错的一部以传统治学功夫统摄消化正史而成的通史。）<br />
<br />
离开书店的时候看见警察押个偷自行车的中年人在报林认现场，警察一直不停地骂着嫌疑犯，还动手要打，见我一直看他，又把手放下去了。<br />
<br />
沿着河往家走，绿杨柳外夕阳已斜，羊肉汤店内飘来阵阵香气，紧了一下步伐，回去给爱妻热饭菜。之后她睡觉，我写书话，写完了就在她身边看书，有什么想法了就对着她熟睡的眼睫毛嘟哝两句，而她则会在梦中回应我，再轻轻地翻一下身。<br />
<br />
这将是多么美好的一个晚上啊！<br />
<br />
（the end 明日略润色后博客放出）<br />
