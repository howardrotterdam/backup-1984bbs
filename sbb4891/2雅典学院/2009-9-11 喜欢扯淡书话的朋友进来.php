出版前言<br />
　　本书为著名中国近代史学者郭廷以先生晚年的重要著作。郭廷以(1903-1978)，河南舞阳人，历任中央大学教授兼文学院院长，台湾&quot;中央研究院&quot;院士，近代史研究所所长等职，毕生从事中国近代史研究，卓有贡献。有《太平天国历法考订》、《太平天国史事日志》、《近代中国史事日志》、《中国近代史》、《郭嵩焘先生年谱》等多种著作行世。这部《近代中国史纲》为著者1969年赴美作学术访问时开始撰写，历时4年，于l972年由香港中文大学出版社出版。该书出版后，即受到学术界和读者重视与欢迎，曾先后3版10次印刷；1991年又在台湾出版发行。这次由香港中文大学出版社授权我社，首次在中国内地出版该书中文简体字版。<br />
<br />
　　本书取材丰富，态度严谨。由于著者与我们所处的社会环境不同，研究视角和方法不同，因此书中对一些历史事件的见解和分析，和内地史学家的一些传统观点多有不尽相同之处，为作者的一家之言。我们出版此书，意在为我国内地读者和研究者提供一部严谨的而又视角不同的近代史著作作参考，以推动这一领域的学术研究的开展。<br />
<br />
　　这次出版的该书中文简体字版，只依照香港中文大学出版社提供的繁体字版本作了简化字处理，书中除对外文人名、地名等译法不同和原版个别错别字作了改动外，其余均未作改动。如有讹误，欢迎指正。<br />
