																											<div id="postmessage_91304" class="t_msgfont">（犬养毅,いぬかい つよし,1855-1932） 日本第 29任首相（1931.12-1932.5），号木堂，冈山县人。庆应义塾肄业。1877年西南战争时为随军记者。1880年创刊《东海经济新报》，提倡贸易保护政策。后得大隈重信知遇，1881睥任统计局权少书记官,始步入政坛。。1882年参加立宪改进党，投身于大同团结运动。1890年第一次众议院总选举中当选众议院议员，连续十七次当选。1898年任第一次大隈重信内阁的文部大臣。1910年创建立宪国民党。1912年领导护宪运动，主张打倒藩阀，拥护宪政。1917年参加寺内内阁的临时外交调查会。1922年组织革新俱乐部。1923-1924年任第二次山本权兵卫内阁、加藤高明内阁的递信大臣。1925年革新俱乐部同政友会合并后退出政界。1929年任政友会总裁。1931年组阁，由于无法解决东三省危机，1931年12月11日，若榇内阁倒台，犬养毅新内阁成立。犬养毅曾对流亡日本的孙中山等人进行过多方关照，和国民政府的很多上层人物都有较好的私人关系。犬养毅打算通过和平手段来解决当时的东三省危机，12月20日，犬养毅秘密派出特使前往南京与中国政府高层会谈。但首相的秘密和谈工作被曝光，激怒了军队内的激进派。1932年3月1日，在关东军的策划下，&quot;满洲国&quot;宣告成立。但当时犬养首相当政的日本政府却没有对“满洲国”予以承认，这更激怒了激进派军人。1932年5月15日，一伙狂热军人居然明火执仗地闯入首相官邸将犬养首相杀害。1932年“五・一五事件”中，被军部右翼分子山岸宏、三上卓等刺死。</div>

							
							
							
							
