十年前在从拉萨飞回北京的飞机上，我的身边坐了一个五十多岁的女人，她是三十年前去援藏的，这是她第一次因为治病而离开北京。下了飞机下很大的雨，我把她送到北京一个旅店里。过了一个星期我去看她，她说她的病已经确诊了，是胃癌的晚期，然后她指了一下床上有一个箱子，她说如果我回不去的话你帮我保存这个。那是她三十年当中，走遍西藏各地，跟各种人---官员，汉人，喇嘛，三陪女交谈的记录。她没有任何职业身份，也知道这些东西不能发表，她只是说，一百年之后，如果有人看到的话，会知道今天的西藏发生了什么。这个人姓熊，拉萨一中的女教师。<br />
<br />
五年前，我采访了一个人，这个人在火车上买了一瓶一块五毛钱的水，然后他问列车员要发票，列车员乐了，说：“我们火车上自古就没有发票”。然后这个人把铁道部告上了法庭，他说：“人们在强大的力量面前，总是选择服从，今天如果我们放弃了一块五毛钱的发票，明天我们就可能放弃我们的土地权，财产权，和生命的安全。权利如果不用来争取的话，权利就只是一张纸。”，他后来赢了这场官司，我以为他会和铁道部结下梁子，结果他上了火车之后，在餐车要了一份饭，列车长亲自把这份饭菜端到她的面前说，还是吃完之后我再给您送过来。我问他你靠什么赢得尊重，我靠为我的权利所作的斗争。这个人叫郝劲松，三十四岁的律师。<br />
<br />
去年我认识一个人，我们在一起吃饭，这个六十多岁的男人，说起来丰台区一所民工小学被拆迁的事儿，他说所有的孩子靠在墙上哭。说到这儿的时候他也动感情了，然后他从裤兜里面掏出来一块皱皱巴巴的蓝布手绢，擦擦眼睛。这个人十八岁的时候当大队的出纳，后来当教授，当官员。他说他所有做这些事的目的，是为了想给农民做一点事。他在我的采访中说到，说征地问题，给农民的不是价格，只是补偿，这个分配机制极不合理，这个问题不仅出在土地管理法，还处在1982年的宪法修正案。在审这期节目的时候我的领导说了一句话，说这个人说的再尖锐，我们也能播。我说为什么，他说因为他特别真诚。这个人叫陈锡文，中央财经领导办公室主任。<br />
<br />
七年前，我问过一个老人，我说你的一生也经历了很多的挫折，你靠什么来保持你年轻时候的情怀，他跟我讲有一年他去河北视察，没有走当地安排的路线，然后他在路边发现了一个老农民，旁边放了一副棺材，他就下车去看，那个老农民因为太穷了，没钱治病，就把自己的棺材板拿出来卖。这个老人就给了他五百块钱让他回家，他说我给你讲这个故事的目的是告诉你，中国大地上的事情是无穷无尽的，不要在乎一城一池的得失，要执着。这个人叫wjb，中华人民共和国总理。<br />
<br />
一个国家是由一个个具体的人构成的，她由这些人创造，并且决定。只有一个国家拥有那些能够寻求真理的人，能够独立思考的人，能够记录真实的人，能够不计利害为这片土地付出的人，能够去捍卫自己宪法权利的人，能够知道世界并不完美但仍不言乏力，不言放弃的人，只有一个国家拥有这样的头脑和灵魂，我们才能说我们为祖国骄傲。只有一个国家能够尊重这样的头脑和灵魂，我们才能说我们有信心让明天更好。<br />
