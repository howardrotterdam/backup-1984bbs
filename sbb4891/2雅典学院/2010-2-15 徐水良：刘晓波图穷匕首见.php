<br />
七，我们不应该指责任何一方是五毛。这样一种说法并无证据和理据，只能使得事情变得滑稽可笑，变成戏谑和噱头，无任何严肃性可言。最后，希望伟大的各派网友都来实行非漫骂的言说和斗争，找出严肃的对敌思维甚至非对敌思维；施行分析和讨论。不要将此地变成一个相声舞台；虽然，我同意笑比哭好。<br />
