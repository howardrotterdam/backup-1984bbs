&nbsp; &nbsp;现代政治学的源头之一是卢梭。卢梭从“人性善”出发，把人类不平等和一切邪恶的起源归结为私有制及文艺科学等文明成果，他提倡回归自然的浪漫主义。他在《爱弥儿》的开篇就说：“出自造物主之手的东西，都是好的，而一到了人的手里，就全变坏了。他要强使一种土地滋生另一种土地上的东西，强使一种树木结出另一种树木的果实……如果你想永远按照正确的方向前进，你就要始终遵循大自然的指引。”“上帝创造万物，都使之为善;而人滥于施为，便成为丑恶的了。”<br />
&nbsp; &nbsp;这种浪漫主义，只具有审美价值而不具有政治价值。由此出发，卢梭式民主来自“公意至上”，带有绝对民主的民粹主义色彩，是个大而化之的伪命题，看似正确而实则荒谬。因为，仅仅从在技术上讲，这种公意很难界定，绝对民主更无可能。<br />
&nbsp; &nbsp;自由民主社会是众说纷纭的社会，从来没有统一的“公意”，至多只有“多数的意志”、“少数的意志”和“相互冲突的意志”。多数意志也大都是集合在某一重大社会问题下的共识，而非集合在所有社会问题下的共识。比如，四十年代的中国，中国的多数意志集合在抗日的民族主义旗帜下，但在解决国内政治的重大问题上，国民党政府并未获得多数民意的支持。再如，现代民主国家每次大选选出的总统，代表的也仅仅是多数民意而非全民意志。而且，其支持者之所以投票给他，也不是因为认同他的全部政见，而仅仅是认同他政见中某一部分。<br />
&nbsp; &nbsp;所以，卢梭的“契约论”最终也就只能走向贤人政治。卢梭论证说，缔结契约就是每个人把个人的一切权利转让给全体。既然每个人都向全体奉献，也就等于没有任何人向别人奉献，人人可以获得平等之权利。人们以交付出天赋的“自然状态”下的平等和自由来换取共同契约中的平等和自由。在这个契约中，全体公民为主权者，国家代表着主权者的最高的共同意志和共同利益；而国家本身无法行使主权，主权的行使必须少数统治者来承担，所以，国家就要由道德贤者来统治，于是，主权在民的公意落实为国家管理权就变成了圣贤的统治权。卢梭式民主走到最后，就变成了典型的“贤者救世论”，在实践中很容易导致个人独裁。事实上，没有分权制衡的制度保证，任何统治者都不会成为“贤者”。相反，有了制衡统治权的制度保证，任何人执政者――无论是贤哲还是不肖者――也不能为所欲为。<br />
&nbsp; &nbsp;从防止公权力不被滥用的角度讲，宪政体制的关键在于：宁要一个可以也不要关进笼子的魔鬼，也不要一个可以为所欲为的天使。<br />
&nbsp; &nbsp;绝对民主在根基上是民粹主义。从历史经验上看，民粹主义首先是一种仇恨情结，他们厌恶贵族、权势者、有产者和知识分子等精英，而同情平民、无产者、甚至流氓地痞，也就是无产业无知识的大众。更有甚者，民粹主义不光是同情，还特别煽情地在道德上无限制贬低精英而无限制地抬高大众，赋予了大众以无穷的创造力和最优越的道德感，最后发展为“大众崇拜”：大众不仅是主人，而且是创造历史的动力。<br />
&nbsp; &nbsp;换言之，民粹主义的正面是“人民崇拜”，反面是打到精英，特别是经济精英和知识精英，所以，民粹主义具有强烈的清教徒主义、反智主义和平均主义的倾向。民粹主义蔑视和仇恨任何有产者，无论是物质财产还是精神财富的拥有者。民粹主义从仇恨物质财富的拥有者出发，进而把仇恨推广到知识文化领域。在他们看来，知识分子也是有产者，拥有并垄断了精神财富，所以，要把物质财富和精神财富的拥有者统统要踩在脚下。在这方面，中国古代传统就具有浓厚的民粹主义色彩，“不杀不足以平民愤”就是这种民粹主义的经典表述。<br />
&nbsp; &nbsp;马克思主义、列宁主义和毛泽东思想中都有浓重民粹主义的成分，马克思的无产阶级最先进、因而最革命的理论，列宁的无产阶级先锋队的理论，都是在道德上拔高无产者的民粹主义。但与马、列相比，毛泽东思想中的民粹主义可谓登峰造极。<br />
&nbsp; &nbsp;许多研究毛泽东的外国学者都认为：毛泽东是一个用马克思主义包装起来的民粹主义者。蒋介石败于毛泽东的主要原因之一，就是蒋不善于利用传统的民粹主义资源，更不善于煽动底层的民粹主义情绪。从战争年代起，毛泽东就毫不含糊地肯定下层民众的造反式革命，称赞流氓无产者的“痞子运动好得很！”他说：高贵者最卑贱，卑贱者最高贵；老农的手和脚最肮脏，但灵魂最干净。他高喊：人民，只有人民，才是创造历史的动力。他进行革命的动员模式是“走群众路线”，他收买追随者的口号是“为人民服务”。<br />
&nbsp; &nbsp;农民出身的毛泽东将传统的民粹主义发展为极端的仇富情绪和反智主义。毛泽东不仅要打到地主和资本家，也要消灭个体性的小商小贩，甚至在人民公社运动中，连农民的那点可怜的“自留地”和自养家禽也要当作资本主义的尾巴割去。同样，毛泽东对知识和文化的拥有者的整肃，也贯穿于他从打江山到坐江山的整个政治生涯。早在延安时期，他就发动了针对知识分子的整风运动，《在延安文艺座谈会上的讲话》变成了指导文艺创作的“圣经”，“为工农兵服务”成为反精英文化的意识形态基础。他要求文学艺术的通俗化和知识精英的平民化农民化无产阶级化，他要把知识人改造成革命者，把文艺和学术作为政治斗争的工具，“以笔做刀枪”地投入暴力革命，进而把知识人变成依附在政治权力之皮上的毛。1949年掌权以后，他更是发动了一次次针对知识分子的政治运动，从思想改造运动到反胡风，从反右到文革，毛泽东式反智反精英反文化运动一步步走向登峰造极。他把知识分子赶到农村、工厂和五七干校，接受大众的改造；他把知识分子打入劳改农场和监狱，剥夺其人身自由；他强迫知识分子在群众大批判中低头认罪，让知识人尊严扫地。毛泽东玩弄知识精英和人性改造的个人欲望满足了，带给中国的却是精神荒漠化和一片文化废墟<br />
&nbsp;&nbsp;凡是现代独裁政权，大都靠民粹主义起家。一般而言，独裁国家的社会分层大都缺少中间阶层，而只有特权化的上层和奴性化的底层，当社会陷入危机或处于转型阶段，奴性化的底层往往变成民粹化的底层。所以，独裁国家处在稳定期，奉行严格的等级制度，而一旦进入统治危机时期，最容易产生民粹主义，民粹主义也最容易被野心勃勃的造反者所利用。<br />
&nbsp; &nbsp;民粹主义在今日中国已经再次出现复活的苗头，“朗旋风”之所以尽吹，与新老左派近年来煽动民粹主义高度相关，从非理性仇富情绪和狂热的民族主义到胡温政权的亲民路线和赎买大众，民粹主义已经成为毛泽东亡灵不死的社会心理基础。然而，当下中国的民粹主义又具有犬儒化的特点――柿子专捡软的捏，仇富情绪只反民营富豪和主流经济学家，民族主义只反美、日、台，二者恰恰放过了今日中国所有弊端的主要根源――独裁体制及其现政权。<br />
