杀人放火守招安，是古代专制统治下敌我互动的一种战术。到了极权时代，不幸不行了。<br />
<br />
也就是说，极权主义没有互动这样一说。于是，企图用和平无敌之战术被招安之人，不是看错了当局，就是错估了政权性质。他们本来要当个瓦文萨啥的，但是，条件不对。瓦文萨自己也没有注意到会有变天一日（见新世纪网）。老哈也是这样。于是，一切在他们估计以外。08等全盘考虑一种双赢，认为制宪者和共产党也会是雅鲁泽尔斯基，所以，实行对于政权的“双合法”抗争。可是这个抗争的前提是错误的。这是第一点。<br />
<br />
第二点，这里的所谓瓦文萨们的战术不一定没有影响，只是这个影响完全对牛弹琴，基本无草根性人民性；刘晓波被捕捉，农工并无很多反映，为证。<br />
<br />
三，凡是主张双合法性者不一定都是五毛。他们只是认为08主张国家主义改革者，终究会被和解和谐到体制里去。所以，大前提就是实行“主体”不明的论说和无主体变奏。<br />
<br />
四，可惜，这样的变奏没有任何自由主义的政府响应论，最后导致政府中人给予他们一个完全的镇压。这样，镇压本身成为被镇压者的道德制高点。全世界都认可这个制高点――但是，这只呈现问题的一半；另外一半，就是老百姓是不是加入这个队列，实行英国宪章主义运动将近两百万人的签署。这是人民认可的挑战。英国和时下中国不同，我们业已论证，不行赘述。<br />
<br />
五，唯一有迹可查者，就是08认为他们得到政府放行，实行和哈维尔见面。但是，在此之后，情形完全改变。老哈说明中国政府比起当年捷克政府要好得多，也算附和这个民间、政府互动说――但是，很快即告破产。等等。当指出：殊不知，七七人也并未知道丝绒会推翻政府。所以，老哈本人是神秘主义者，认为历史不可预期――就是，他自己认可的：他也不知道会当政。<br />
