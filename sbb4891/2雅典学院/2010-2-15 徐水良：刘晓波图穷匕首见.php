三哥：警惕中共媒体之反民粹主义<br />
<br />
&nbsp; &nbsp;（本文正文原标题：在博讯《姜力钧文集》中是《中国天网人权事务中心新闻稿》，不包括附件）<br />
&nbsp; &nbsp;2007年，中国大陆民间维权迅速波及全国各个角落的一年，也是天网人权义工艰苦抗争、前赴后继走入班房的的一年。<br />
&nbsp; &nbsp;2008年2月1日，中共官方媒体《人民网》等陆续推出【2007中国互联网舆情分析报告】，称“民众维权意识空前增强，但需警惕民粹心理”。<br />
&nbsp; &nbsp;该报告还引述“法学家”之口借重庆钉子户事件称“如果说连法院也不理睬，法院裁决了我也不动，这是民粹主义，而不是以民为本。”<br />
&nbsp; &nbsp;《人民网》要干什么？<br />
&nbsp; &nbsp;人民网号召警惕民粹心理，喉舌跟进<br />
&nbsp; &nbsp;众所周知，当今中国的司法系统并不独立，公检法司都归当地中共党委一元化领导，法院只不过是地方党委的一个分支机构，任何一个法官乃至法院院长都没有独立审判的权力。在这种没有司法公正、缺乏舆论监督前提下的所谓“法院裁决”，乃是官僚及专制主义“精英”剥夺千百万草根民众利益的遮羞布。<br />
&nbsp; &nbsp;有趣的是，中共《人民网》警惕民粹主义后，一些外围媒体出于各自的目的，立即加入了大合唱，其角色不言自明。<br />
&nbsp; &nbsp;2007年，我们与维权民众在海内外、体制内外良知人士帮助下共同取得了造福湖南花垣县20多万民众的【大批武警彻底摧毁湖南花垣非法矿洞】、【深圳元升工人维权获3000万】等诸多重大胜利。11月6日，温江农民拉开中共建国以来第一起农民夺回土地大行动【石破天惊成都数百农民夺回土地做主人】。为防止夺回土地做主人行动迅速波及全国，幻想“始终代表”的温江区党委书记李刚先生不仅指责农民维权为民粹主义，还信口雌黄“六四天网是李洪志在美国投资几十亿搞的，是专门反对共产党、勾结境外敌对势力的【组图：天网义工组团前往温江考察灾情】。”<br />
&nbsp; &nbsp;而这一切都是与冻结天网人权资金、屏蔽天网人权网站同步的。<br />
&nbsp; &nbsp;从抓捕高智晟、郭飞雄、严正学等朋友，海内外各界已经看清镇压维权人士的基本套路：文人抹黑，外围媒体传播，抓捕判刑。<br />
&nbsp; &nbsp;不过，官方与外围媒体的合唱，不仅充分证明民间维权正在中国大陆取得巨大成功，也让海内外朋友明白：在中共官方媒体不便也不敢发声的问题上，谁是抹黑民间维权、镇压异议人士的先锋。<br />
&nbsp; &nbsp;我们认为：成都读书会书友、天网人权义工、辽宁蚁力神灾民、被非法拆迁或被依恶法强制拆迁户、北京及全国各地的访民不是民粹主义；我们认为：成都失地农民、黑龙江、吉林、河南的失地农民、山东烟台失地农民、江苏无锡失地农民以及全国各地农民维权也不是民粹主义；我们认为：深圳外资工人、湖南云梦工人、重庆矿业工人、攀枝花公路工人、各地石油工人以及全国各地工人维权也不是民粹主义；我们认为：云南被买断工龄干部、各地军转干部、各地民办教师、各地买断工龄银行职工的维权都不是民粹主义……<br />
&nbsp; &nbsp;总之，“做托的同志们”应该明白，当今中国的首要问题是专制主义而非民粹主义。迄今为止，我们在全国范围内也没有发现一个维权群体是什么民粹主义。同时，我们更应高度警惕的是，那些虚拟各种理由阻挠中国进行政治体制改革的既得利益集团将以反“民粹主义”为借口，对风起云涌的非暴力维权运动展开新一轮围剿和镇压。<br />
&nbsp; &nbsp;显然，那些污蔑中国民间维权的御用喉舌有如螳臂当车，不仅无法改变9年天网“与无权、无势、无名的弱者同行”的坚强决心，也挡不住中国民间维权飞速前进的坚定步伐！<br />
