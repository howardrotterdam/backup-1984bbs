金雁：俄国革命与民粹主义<br />
<br />
&nbsp; &nbsp;十月革命是在马克思主义口号下民粹主义的大释放<br />
&nbsp; &nbsp;我们现在常说，十月革命是马克思主义在俄国的实践，列宁主义是对马克思主义全面的继承和发展。其实，马克思主义远离了先进的欧洲故乡，在向落后国家的胜利进军中，首先被文化背景大不相同的俄国改造，以列宁主义取代了马克思主义。在还没有出现“列宁主义”这个名词的时候，当时各种立场的人是如何看待这一问题的？我们由于意识形态的原因，以权力等同于真理，把符合人类长远利益的科学精神变成了政治功利主义，或者人为塑造成封闭的宗教神学，对列宁主义的认识一直处在“政治正确”氛围中。<br />
&nbsp; &nbsp;以普列汉诺夫为首的老一代马克思主义者认为列宁是对马克思主义的背叛，“俄国人如果认同了列宁在1917年的主张，只能证明这30年在俄国宣传马克思主义是失败的。”米留科夫说，相对于欧洲无产阶级革命来说，列宁、托洛茨基领导的这场运动更像普加乔夫。切尔诺夫认为，列宁主义就现实来源接近特卡乔夫要多于接近马克思。特卡乔夫曾断言，“将在俄国发生的革命决不会按西方的方式进行，因而不能套用马克思主义，资本主义不发展正是社会主义革命的有利条件，俄国沿着民粹派的道路走下去，不会成为资产阶级立宪国家”。实际上列宁自己也称十月革命为“农民式的革命，农民式的大民主”。别尔嘉耶夫认为，俄国革命的胜利，使原本不具备俄国特点的马克思主义，获得了几乎与民粹主义、斯拉夫主义接近的风格，俄国彻底转向东方的源头在于列宁。“民粹主义换了一种形式进入到列宁主义中来”。俄国共产党宣言的起草者司徒卢威说，表面上看与民粹主义进行斗争的是马克思主义，实际上是战无不胜、所向披靡的民粹主义吸引并同化了马克思主义，民粹主义者与马克思主义者之间的差别至多是政治纲领和社会学意义上，这种差别完全不具有文化哲学意义上的差异，在道德上俄国马克思主义秉承了民粹主义功利主义倾向，“数十年来他们始终保持着民粹主义的精神气质”。意大利共产党创始人葛兰西认为，列宁领导的“布尔什维克革命是一场否定马克思《资本论》中某些观点的革命”。<br />
&nbsp; &nbsp;对这些评价我们完全可以用这是反列宁主义的诽谤来予以回答，但是要看到上述人物立场各异，但是他们的共同点在于都认为，在马克思主义和民粹主义之间，列宁主义更接近于后者，客观地说，这个评价是有其深刻的社会背景的，要驳倒它决非易事。中国理论界最常见的答复是，俄国马克思主义是从民粹主义脱胎出来以后就与其决裂，并与自由主义联手对民粹主义进行了猛烈的批判。<br />
&nbsp; &nbsp;这的确是事实，早期的俄国社会民主党对民粹派的五大特点（1、俄国特殊论。2、政治上的人民专制。3、经济上的村社社会主义。4、手段是为目的服务的。5、黑社会式的精英党）都极端厌恶，并且完全与之划清界限。首先，公开宣称自己是西化论者，文化论战中的西方派，自称“西方社会主义”，普列汉诺夫的名言是：“特殊论”是停滞和反动的同义词，俄国的进步力量都凝聚在西欧主义的大旗下，我们是自由主义之友，民粹主义之敌。<br />
&nbsp; &nbsp;第二，民粹派奉行精英领导群氓，用特卡乔夫的话说“革命”就是把少数人的意志强加在习惯在黑暗中生活的群氓身上，车尔尼雪夫斯基说，革命成功后如果“人民需要沙皇的话，可以再造一个沙皇”。社会民主党反驳说，政治自由、政治民主是真假马克思主义的试金石，民粹派扭曲个人的独立价值扼杀个人精神自由并不亚于沙皇。<br />
&nbsp; &nbsp;第三，民粹派认为只要革命消除了“个人主义”，原本深藏在农民身上的“社会主义”本能就会显露出来，加以整合自然会走向统一意志驱动下的集体经济。普列汉诺夫认为，这是皇帝――国王的国家社会主义，是宗法式的社会主义，人身依附下的公有制是全能专制国家的基础，马克思的集体是自由人联合体，个人并不淹没在集体之中，民粹主义的集体主义是压制个人的集体，扼杀个人的集体，以集体的人格代表对个人实行专政。要发展独立农庄，走市场经济，土地只能在中农中间进行分配，决不能分到贫农或无法成为农场主的懒惰农民手中。<br />
<br />
<br />
 第四，民粹主义行为方式是“通过‘恶’达到善”，为达到目的不择手段，为了最终目的一切都可以忽略不计，负负得正，“以恶抗恶就可以得到善”，否则君子永远斗不过小人。俄国社民党坚决谴责这种“涅恰耶夫式”的工作作风。普列汉诺夫认为，一定要脱离黑帮习气，目的正确手段要光明正大。<br />
&nbsp; &nbsp;第五，在党务活动上以民意党为榜样，党的领袖是绝不允许背叛的绝对权威，精英党要高度集中、严守纪律，限制党内争论。这就是孟什维克和布尔什维克在党建问题上的争论的核心，受第二国际影响的孟什维克认为，德国社民党为我们树立了榜样，民主建党、政教分离、允许党内争论、有派。列宁受其兄亚历山大&#8226;乌利扬诺夫的影响在对民粹派理论口诛笔伐的同时，却对民粹派组织模式情有独钟，认为那种集中、秘密、纪律、限制争论的组织是党必须坚持的，他的解释是，由真正的革命家组成的党组织越坚强，党内的不坚定性和动摇性就越少。“共产党只有……在党内实行近似军事纪律那样铁的纪律，才能履行自己的职责。”我们需要的是一个“把俄国翻转过来”的政党，“西欧型的社会民主党不适合俄国准备夺取政权的任务，因此俄国需要新型的革命的马克思主义政党”。列宁称赞民意党是“我们大家应当奉为模范的出色组织”。列宁的做法被同是社民党内的左派的托洛茨基、卢森堡等人认为是搞“农奴制”、“搞戒严”，正是这种党务方式与行动准则为布尔什维克向民粹主义的过渡埋下伏笔。可以说除了这最后一点有分歧外，俄国马克思主义者对民粹派纲领性的东西都逐一进行了批判。<br />
&nbsp; &nbsp;1917年由于斯托雷平改革造成的特殊的历史时刻，布尔什维克对民粹主义的扬弃发生了颠倒，从自由主义之友、民粹主义之敌变成了民粹主义之友、自由主义之敌，从批判“人民专制”走向无产阶级专政，从弘扬政治自由走向践踏政治自由，认为“民主妨碍共产主义世界观的建立，在公民权利和自由习惯缺失的专制国家比西方民主国家更容易实现无产阶级专政。”从反对土地国有、村社化到“把村社放在第一位”。列宁《俄国资本主义发展》1899年、1908年两个版本：从“村社对农民的危害很大”到“解散村社对农民的危害很大”；从6个自由到4个禁止；从反对俄国特殊论走到和民粹主义抢“俄罗斯民族特殊论”的潮头，最后把原来的师傅挤下历史舞台，1917――1918年对民粹主义的三次大分化，联合左派社会革命党打击社会革命党，联合最高纲领派、村社共产党镇压左派社会革命党，每次都是取其原教旨主义、宗法性强的一派打击自由主义和社会民主主义的一派。一直到把社会革命党全部关进监狱，一直到对封建主义、农民、资本主义、议会主义等一系列问题发生重大的变化。（十月革命中颁布的土地纲领都是社会革命党的。）<br />
&nbsp; &nbsp;之所以会出现这种局面是斯托雷平改革改变了俄国的利益格局，使俄国的政治派别发生了很大的变化。由于斯托雷平改革的不公正性、掠夺性、以牺牲广大农民的利益而保证向权贵资本主义道路上发展，强烈地触发了人民的反改革情绪，俄国的反体制运动于是从知识分子方面转到了下层民众方面。怎么看待这场群众自发的“民粹主义”运动呢？普列汉诺夫认为，这是个罕见的历史时刻，农民想使历史车轮倒转，但鉴于它具有反暴政的革命意义，也就是说农民反改革的村社复兴运动是推翻沙皇的革命因素，对此我们采取宽容的态度不去反对就是了。列宁说不对！在经济上反动的东西，在历史上可能是正确的。我们不但不反对，还要站在这场运动的潮头来领导它。从夺取政权考虑，首先对民粹派的态度要转变，但是仍然要斥责他们，这是争夺领导权所必要的，过去我们说他们的纲领不行，现在我们要说他们是自己纲领的背叛者，换句话说，过去我们是站在社会民主主义的立场上批判他们，现在我们要站在“超民粹主义”的立场上批判他们。普列汉诺夫反对说，如果对这场运动持一种民粹主义的赞赏态度，那么这场以“人民名义”完成的革命的后果将是：1、政治上的怪胎，是一个共产主义基础上革新了的皇帝专制，2、革命后公社所有的土地将比现在多3倍，这样就会成为向更高级社会形态发展的障碍，虽然从历史否定之否定的角度似乎会更容易，但中间的缺课，“超越历史阶段”早晚要补课的，3、革命后我们又回到自然经济，但同时西方也就不能影响我们，俄国的传统色彩将会更浓。<br />
&nbsp; &nbsp;在这一过程中，列宁主义从党务上的民粹主义发展到意识形态的民粹主义，由否定民粹主义到肯定超民粹主义，从而完全脱离了社会民主党的轨道。普列汉诺夫说，我们和旧列宁没有分歧，与新列宁的分歧在于，他推翻了他过去推崇的东西，而赞成他过去反对的东西。季诺维耶夫说，我们看到了一个奇怪的化妆舞会，无产阶级革命党指责的社会革命党的人民专制、国家社会主义，现在恰恰是他自己正在大力倡导的，也就是说，我们现在认为的社会民主党和共产党这两支左派的分歧（第二国际和共产国际），被我们一直奉为正朔的“共产党”有很多内容都是民粹主义的。<br />
&nbsp; &nbsp;俄国民粹主义的缘起<br />
&nbsp; &nbsp;经典民粹主义以俄国民粹主义为代表，它是我们认识民粹主义的门径。要想了解俄国民粹主义，首先要了解俄国的传统农村。<br />
&nbsp; &nbsp;俄国农村是一个公社的世界（在俄语中米尔――村社和世界是同一个词），农民则是米尔公社社员，米尔实行土地公有、定期重分，有劳动组合和共耕地，并且实行“征税对社不对户，贫户所欠富户补”的连环保制度，具有村会选举和村社审判等“小共同体”的自治功能，甚至住宅都必须建在一起。让我们中国人不可想象的是，米尔成员对村社的义务要超过对国家的义务。共同体给每个人安排了不容竞争也不可改变的位置，也就是我们通常所说的“束缚――保护”功能的统一，由此形成了一种“俄国式的集体主义传统”，既对抗国家主义也压制个人主义。<br />
&nbsp; &nbsp;这种传统对俄国社会文化有极大的影响，使之形成了深层的历史积淀：首先，村社的平均意识、轻商抑商传统与“不与私有化共谋”的民族理念，“宁可全部土地归沙皇，只要不归地主”的村社观念更直接产生了“好沙皇”情怀。俄国人常以鄙视的态度看待经商者，认为那只不过是“贪婪的犹太人的营生”，在民族认同中有普遍的仇富心理和反西方传统，他们认为，富人都是恶棍，财富是腐蚀人类灵魂的东西，从心底里向往一种贫穷和朴实无华的社会模式。<br />
&nbsp; &nbsp;其次，在民族性格中，村社生活使俄国人重视邻里关系的和谐，这使俄国人成了一个重感情的民族。因此俄国人性格外向，浪漫有余理性不足，所以近代俄国在文学史上群星灿烂，却罕有伟大的哲学家。处事行为中浪漫有余、易大嗡大轰、一蹴而就，缺乏理性思维、缺乏妥协精神和持久耐力，总是力求用快捷的方式解决问题，因此容易导致群众性歇斯底里运动。<br />
&nbsp; &nbsp;再次，村社的集体观念导致的抽象的人民崇拜、体验苦难、底层意识和土地崇拜，对抽象人的“爱”和具体人的“恨”是并行不悖的，在精神共性中独立个人是不和谐的原子，“爱”大多数和“恨”个人是可以自然转换的，由此又导出了从宗法角度对西方民主自由的鄙视，民粹派的一句名言是：“自由的概念只与集体有关，个人对集体来说是没有自由的，对个人说，自由就是对集体的服从，只有把个人溶于集体中才能获得最大的自由。”<br />
&nbsp; &nbsp;还有，在东西两大文明夹击下造就的独特的民族诉求和强国诉求，决定了它的“民族身份辨认性”较为突出，有强烈的弥赛亚（救世性）情结。总之，在村社里，平均与剥削、“民主”与专制、权利与义务取得了美妙的协调，同时又体现了斯拉夫传统，因此在俄国根深蒂固。<br />
&nbsp; &nbsp;19世纪上半叶随着农奴制危机的加深，反抗农奴制的革命民主主义在平民知识分子中蔓延，赫尔岑、车尔尼雪夫斯基等人主张农民连同土地一起解放，通过自己特殊的农民村社，走上一条非资本主义、非西方发展模式的农民社会主义理论都非常流行。1861年自上而下的农奴制改革，因为改革的方式极不公正，贵族以割地形式把部分村社土地攫为己有，农民还需为得到另一半土地缴纳赎金，从这个意义上说，农奴制改革与其说是农奴得到了自由，不如说是地主得到了自由，改革以后地主经济快速地向资本主义经济迈进，它的发展方向是以追赶欧洲的现代化为目标，但是却以牺牲俄国农民利益为特征，农民不但仍然处于无权的地位，而且原来的温情世界被打破，贫富分化快速加大，农民在失望中掀起了抵制改革和争取土地与自由的斗争。<br />
&nbsp; &nbsp;与社会情绪激进化相应的是平民知识界发起了“民粹主义”运动，他们积极充当农民利益的代言人，反对这种非人道的改革，要求恢复原有“抑强扶弱”和社会平等的传统。在他们看来，资本主义发展在俄国不是历史的必然，只是一种偶然现象，是不和谐、是祸害、是倒退和腐蚀人类灵魂的污泥浊水。俄国传统中的“平均”和“民主”是极其宝贵的财富，在村社集体主义的土壤上培养出的“社会主义”是对抗西化、自由化、资本主义的天然屏障，是通往理想王国的桥梁。他们“相信俄国生活的特殊方式，相信俄国生活的村社制度，由此相信农民社会主义革命的可能性”。他们认为实现社会主义的主要力量不是工人阶级，而是“按本能、按传统来说是共产主义者”的农民，因而农民是俄国革命的动力。他们还说历史是英雄创造的，整个人类的进步是“芸芸众生”盲目地跟随“那些能够批判地思维的精英”。<br />
&nbsp; &nbsp;民粹主义的“村社社会主义”反映了俄国农民反对农奴制和沙皇专制制度的要求，争取土地和平等的朴素愿望，用恩格斯的话说，在政治上是正确的，他们为了实现自己的革命理想不惜抛头颅洒热血，谱写了可歌可泣的壮丽篇章，但是他们的主张在经济学形式上是错误的，他们不懂得历史发展的客观规律，企图“倒转历史车轮的”，“宁肯让农民继续停留在因循守旧的宗法式的生活当中，也不要在农村中给资本主义扫清道路”的想法是愚蠢的、会被时代抛弃的。<br />
&nbsp; &nbsp;纵观整个俄国历史上的民粹主义可以分为理论、思潮、运动、体制，四者间又独立又联系几个方面，又有革命民粹主义、自由主义民粹主义、警察民粹主义、社会革命党、新民粹主义等五种类型。<br />
&nbsp; &nbsp;民粹主义研究在我们目前的党史教科书中有一些“盲点”，以往的民粹主义研究是基建于“革命民粹派”与“自由主义民粹派”两个概念上的，据说19世纪70年代的民粹派是革命民主主义，而80－90年代民粹派逐渐“转到自由主义的立场上去了”，成为堕落的“自由主义民粹派”，从而标志着民粹派运动的衰亡。所以因为一般历史教科书中的介绍，人们对巴枯宁、特卡乔夫并不陌生，对丹尼尔逊、沃龙佐夫也有所闻，其实现实远非这么简单，就是仅就人员组成来说也一直延伸到20世纪30年代。<br />
“两段式论”后面最突出的是“警察民粹派”，它的特点是它的“警察性”，即政治上的专制倾向。它打着“人民的”、“社会主义的”旗号反对民主政治中的政治自由、议会民主、公民权利等原则。他们声称立宪政治只是富人的工具，贫富不均条件下的民主是虚伪的，西方的自由与民主是抽象的、毫无疑义的，是政客们腐败的游戏，根本不具有什么普世价值，西方的政党政治是“有害于人民群众本身利益的”，人民当家作主不过是皇帝的新衣，远不如一个高高在上的主宰来“抑强扶弱”、“为民做主”更合乎正义，所以社会主义者甚至可以认为，对于人民来说，在两害中间，专制的沙皇毕竟比立宪的沙皇要好些，因为它可以使那些“贪婪的私有者”害怕。选举选出来的都是富人，富人管事很不公道，俄国的统治机关不是选举的，一切有专制沙皇来管，沙皇高于一切人，既高于穷人又高于富人，因此对一切人都是很公道的。<br />
&nbsp; &nbsp;提出这种理论的往往是一些所谓的正统理论捍卫者，他们把“人民专制”奉为理想模式，在他们看来法国的雅各宾专政这个标本应该广泛推广，对不顺服这个专制者将实行顺我者昌、逆我者亡的原则，“谁不和我们站在一起，谁就是反对我们，谁反对我们谁就是我们的敌人，而对敌人就应该用一切手段予以消灭。”<br />
&nbsp; &nbsp;十月革命后苏联对民粹主义的批判一直都很高调，但是细观起来只是在打击民粹主义旗号下批判他们向自由主义转化的一面，直到1930年在“劳动农民党”案件中新民粹主义的主要人物被从肉体上消灭为止，虽然在俄国以“民粹主义”命名的活动不再存在了，但是民粹主义本身、尤其是警察民粹主义的那些价值反而在斯大林体制的框架下得到空前强化。以往的研究有很多误区和错误论断，比如“两段式论”、有些代表人物的定位，一边接过他们的思想一边批判他们背叛自己的纲领，甚至把在苏维埃政权下党外人士对社会主义的探索都网罗在“民粹主义”的罪名下，给人造成一种假象，好像苏联批判民粹派很卖力，但实际上是在列宁主义的旗帜下把民粹主义的消极一面更加发扬光大。<br />
&nbsp; &nbsp;革命民粹主义<br />
&nbsp; &nbsp;革命民粹主义，也叫“积极行动的民粹主义”。1861年农奴制改革的结果使原来对沙皇怀有期望的俄国知识分子大失所望，纷纷从“利益协调倡导者”转向“积极的行动者”，他们对暴力革命的狂热日益加剧，以革命颠覆主张取代了改良，“暴力变革”的公式已为当时的民粹主义者普遍接受，至此民粹主义从一种理论思潮扩展到运动，成为俄国的政治舞台上的重要角色。史学界一般把革命民粹主义运动分为大体上以十年为基数的三个阶段：小组阶段、到“民间去”运动以及“民意党”的恐怖活动阶段。（这是一般教科书都会提到的）<br />
&nbsp; &nbsp;60年代末是小组活动时期，民粹派的小组遍布于各大学，他们受巴枯宁、柯鲁泡特金、拉甫洛夫等人“知识分子对人民欠有无法偿清债务”和“农村才是战场”理论的影响，到70年代的运动是以“到民间去”的形式表现出来的，数千名革命青年和一些贵族到民间去，策动农民造反，全国至少有37个省卷入这一运动。这可以说是民粹派学说的第一次全面检验。民粹派相信农民中蕴藏着“共产主义的本能”，用三两年时间很快就可以完成“社会革命的”目的。但是这一腔热血很快在农民面前碰了壁，知识分子与农民之间无法逾越的鸿沟使农民本能地排斥这些“精神失常者”的行为，农民仍然是“好沙皇”的信奉者，仅1874年就有一千多名“宣传员”被捕，具有反讽意味的是许多民粹派是被当地农民告发而被捕的。从1873――1879年因“社会革命宣传”案件而受审判的人就有2500人。在沙皇和农民的夹击下，“到民间去”运动遂告失败。<br />
&nbsp; &nbsp;运动失败以后，民粹派首先意识到必须把各个小组联合起来，于是1876年成立了一个秘密的民粹派团体――“土地与自由社”。该组织最大的特点是它奠定了我们耳熟能详的“集中制”组织原则的雏形：秘密、集中、互相监督、少数服从多数、严守纪律（在此之前涅恰耶夫也曾提出过要以极端专制、铁的纪律的小型革命支部覆盖全国，但是没能付诸实践）。著名的“土地与自由社”与后来的“民意党”领导人之一，亚&#8226;德&#8226;米哈伊洛夫说，正是从这个时候起，秘密活动变成了一门“完整的科学”，这门科学对与专制制度作斗争特别适用。“土地与自由社”失去了到农村发动群众的热情，便把全部希望寄托在少数“革命家”同政府的拼死一搏上，以个人恐怖活动作为主要的斗争手段，70年代末到80年代初发生了多起刺杀沙皇和反动官僚的事件。<br />
&nbsp; &nbsp;1879年“土地与自由社”分裂为两个独立的组织：“民意党”与“黑色土地平分社”。民意党是80年代革命民粹主义的主要组织，该组织把恐怖手段视为最重要的“斗争形式和生命力”。在党纲第五条第二款上专门论述“破坏与恐怖活动”的必要性，它明确指出要实现任何途经的变革，都离不开“独立革命的成果和借助于密谋取得的成果”。它的另一大“贡献”是发展了土地与自由派的“黑社会式”组织原则，并且鼓励采用以任何方式同“恶”作斗争，在他们看来将来的“善”是通过现在的“恶”来达到的。1884年民意党停止活动，传统上认为的革命民粹主义运动结束。<br />
&nbsp; &nbsp;革命民粹主义者的分化，使部分民粹派从目的和手段上向两个完全不同的方向转化，成为完全对立的派别，这就是后来的马克思主义与“警察民粹主义”。1882年“黑色土地平分社”分裂成一些小组，以普列汉诺夫、阿克雪里罗得、查苏利奇等人经过几年的探索以后转向了马克思主义，他们在斗争方式上仍然是“革命”的，但是革命的目的不再是民粹派的反对政治自由的“人民专制”和反对市场经济的“村社社会主义”。<br />
&nbsp; &nbsp;就在同一年“民意党”在沙皇的严厉镇压下彻底失败，幸存的中坚分子大都流亡国外。他们中最狂热、最铁杆的革命分子吉霍米罗夫、卡拉马洛夫等人经过一段反思以后发现，其实沙皇政府中最反动的那一部分人也和他们一样热衷于弘扬“村社集体主义”精神，抵抗西方个人主义的瘟疫，恢复斯拉夫民族特殊的传统。既然革命无望，何不转而依附当局，于是公开出版了《我为什么不再做革命者？》理论性忏悔著作。这些人回国后一头扎进保皇主义的阵营，从革命的圣斗士变成了传统的卫道士，很快完成了从“雅各宾的民粹派”到“波拿巴民粹派”的转变。从这个意义上说他们不再是革命者，但仍然坚持民粹主义传统价值。<br />
&nbsp; &nbsp;谈革命民粹主义一般都要提到的三个主要代表人物分别是特卡乔夫、巴枯宁和拉甫洛夫。这三个人实际的理论诉求差异非常大，以后的分化使其追随者走上完全不同的道路，但是在当时他们在坚持革命和相信农民社会主义革命的可能性以及相信村社制度这些基本点上是一致的。<br />
&nbsp; &nbsp;文学评论家特卡乔夫，他一生中主要受三个人的影响：他们分别是国内的涅恰耶夫、车尔尼雪夫斯基和国外的布朗基。特卡乔夫是个狂热的俄国的布朗基主义者，他对国内的资本主义发展充满了恐惧和不安，认为必须马上、毫不拖延的进行变革，任何犹豫不决、任何拖延耽误都是犯罪。如果现在不干革命，资本主义的发展将推迟革命，也许永远就干不成了，革命家不是准备革命，而是干革命。他把涅恰耶夫“人民惩治协会”“以恶抗恶”那一套发挥到极致，一般认为“民意党”的纲领和斗争方式在很大程度上来源于特卡乔夫，而布尔什维克又是民意党纪律的坚决主张者，列宁当年深受其兄民意党敢死队员亚历山大&#8226;乌里扬诺夫的影响，对民粹派的集中、秘密、纪律、限制争论组织模式十分欣赏，并明确指出“民意党”是“我们大家应当奉为模范的出色的组织”。<br />
&nbsp; &nbsp;“暴动派”巴枯宁在立即革命这一点上与特卡乔夫不谋而合，鼓吹“热爱破坏就是热爱建设”，人天生是要造反的。当有人问道，如果他成功地实现了他的计划，创造了他想要创造的一切以后还会做什么？巴枯宁回答说“那我就立刻开始又一次摧毁我所创造的一切”。但在暴动的归宿问题上两人却有原则的分歧，巴枯宁着眼于农民，特卡乔夫则看中的是“少数密谋分子”。<br />
&nbsp; &nbsp;温和的具有人道倾向的贵族出身的数学教授拉甫洛夫被认为是70年代革命民粹主义中的“右翼”，传统上把他叫做“宣传派”、“准备派”。其实在反对专制制度问题上，他不但主张直接采取政治行动，甚至在必要的时候他并不反对使用“恐怖手段”。他1882年加入“民意党”，并认为它是俄国唯一能够培养出社会力量的革命党。他参加第一国际和巴黎公社，与马克思、恩格斯相识并深受其影响，是俄国早期民粹派中最接近马克思主义的革命家，他的许多论述对普列汉诺夫等人脱离民粹主义转向马克思主义有至关重要的作用。他对俄国革命的作用有其民粹派中少有的冷静与理智。他认为不能通过人工的革命去“促进历史，”即便能够获得胜利，历史的欠债总是要补偿的。他反对当时在革命家中流行的以“为达到目的一切过程都可以忽略不计”的阴谋策略，更反对特卡乔夫的那句名言“我们考虑的是应该留下多少人”。他认为“在社会革命中暴力是必要的。但是应该尽可能地把它降低到最低限度，如果社会的一切物质力量转到了获胜了的政党手中，而它的党员占居民的绝大多数，新制度的敌人仅在思想上和精神上拥有势力的话，那么对敌对分子采取的强制手段也可以降到最低限度。不管新的暴力政权以什么为基础，我们不希望以新的暴力政权代替旧的暴力政权”。<br />
&nbsp; &nbsp;革命民粹派发展的最高阶段――社会革命党<br />
&nbsp; &nbsp;19世纪80――90年代民意党失败以后，俄国又出现了新一轮的民粹派组织，它们多以“社会革命”来命名，这是因为在当时“社会革命”与“积极行动”几乎是同义词。这些“前民意党人”的重新组合过程是，先产生了一系列的地方性团体，其中最著名的是以切尔诺夫和斯列托夫组织的“农业社会主义同盟”，1902年就以这个团体为基础，在海外成立了全国性的社会革命党。1905年12月社会革命党召开第一次代表大会，通过了党纲和党章并表明自己的立场是，俄国是一个农民的国家，作为农民的政党其目的是推翻专制制度，建立民主共和国，把土地变成全民的社会财产交给农民平均使用。<br />
&nbsp; &nbsp;社会革命党并没有承袭“民意党”的高度集中的“黑社会式”组织模式，由于加入第二国际，受西欧社民党的影响，他们采取的是“组织上的虚无主义和党内民主，没有硬性明确的组织界限和强制性的纪律约束”。这种组织上的“联邦制”色彩，导致该组织纵向联系松散和团体内部的多元性，斯托雷平改革后更具体地分为以切尔诺夫为代表的中派、具有自由主义倾向的“劳动派”以及具有无政府主义激进色彩的“最高纲领派”。对此切尔诺夫总结说，社会革命党和布尔什维克共同“瓜分”了民粹派的遗产，布尔什维克得到的是铁的纪律约束的“组织原则”，而社会革命党则继承了“恐怖主义”搞暗杀的斗争手段。仅在1905――1907年革命的两年半时间里，社会革命党就搞了200多次恐怖活动，以至于这一时期，沙俄 “社会革命党”正像它的名称一样，以“毫不妥协的革命立场”为争取“土地和自由”在进行不懈的努力。与民粹派运动低潮时期的妥协倾向的“自由民粹主义”、“合法民粹主义”乃至与沙皇合流的“警察民粹主义”截然不同，它的革命性不仅没有衰落，而且如果把“革命”理解为激进的反抗立场的话，在斯托雷平的反村社改革激起了社会上“村社复兴运动”的新条件下，一贯具有村社捍卫者形象的社会革命党重新成为当时最典型的“革命者”。<br />
&nbsp; &nbsp;在三次革命当中，社会革命党发挥了极大的作用。二月革命后社会革命党支持并参加临时政府，克伦斯基先后担任临时政府司法部长、海陆军部长和总理，切尔诺夫任农业部长。这时社会革命党的影响超过了其他一切政党（包括布尔什维克），成员多达一百万，是当时俄国最大的政党，并控制工兵代表苏维埃和农民苏维埃以及地方新政权。在十月革命中他们与孟什维克、布尔什维克共同组建彼得格勒革命军事委员会，有7人参加委员会，2人进入核心局，同时也是许多地方起义的领导人。<br />
&nbsp; &nbsp;一战以前社会革命党内部各派没有什么原则分歧，他们主要在国家杜马和农村组织方面工作。一战爆发以后部分社会革命党人因不同意党中央的护国主义立场导致内部分歧，1917年12月左派社会革命党分裂出来成立独立的政党，与布尔什维克建立同盟。但是两者的合作是短暂的，先在布列斯特和约问题上发生了“最尖锐最剧烈的冲突”，左派社会革命党在1918年讨论“和约”问题的苏维埃第四次非常代表大会上退出人民委员会，紧接着，因不同意布尔什维克政府实行“粮食专卖法”的粮食垄断政策与其彻底决裂。“三仙巷事件”以后，左派社会革命党在布尔什维克的全面封杀下分裂，其左翼组成了村社共产主义党和民粹派共产主义党继续与布尔什维克合作，其他人转入地下活动。1923年社会革命党宣布解散，切尔诺夫等人流亡国外（他们仍在办杂志探索社会主义模式，切尔诺夫写了一系列很有价值的专著，但是毕竟只是“事后诸葛亮”。由于封杀和排斥，我们也只是在苏东剧变后才看到这些深刻的总结）。<br />
&nbsp; &nbsp;社会革命党在斗争形式上依然保持了“革命民粹主义”的斗争性与革命性，但是作为一种理论却与革命民粹主义的正统形态发生了很大的变化。这是因为民粹派正统理论具有害怕“时间对俄国革命不利”的“时间恐惧症”，随着时间的推移，资本主义发展使俄国在20世纪初跃升世界第五位，成为中等资本主义国家，有关绕过资本主义的“俄国特殊道路”之说信奉者日少，形势已经把严重的问题摆在了面前：村社解体了，民粹派怎么办？在俄国已经走上资本主义的情况下，“村社社会主义”还有立足之地吗？社会革命党的理论家在对革命民粹主义和自由民粹主义理论和实践进行总结之后，形成了自己的行动纲领和理论，它是对民粹主义正统理论的大修正。<br />
&nbsp; &nbsp;第一，以“劳动原则”取代“公社原则”。我们前面讲过，传统民粹派对农民的尊崇具有浓厚的整体主义色彩，他们推崇的是农民公社，是“公社世界”中的农民社会，而不是农民个人，他们对一个个具体农户的“个人主义”要求持敌视的态度，因此在农民的两大美德即“劳动”与“集体主义”中，传统民粹派最看重的是后者而不是前者，他们历来是把“公社原则”置于“劳动原则”之上的。而社会革命党则认为，劳动战胜剥削是社会主义革命的首要阶段。<br />
&nbsp; &nbsp;第二，提出了最低纲领与最高纲领的区别，明确革命可以分两步走。第一步首先应该实现的是“人民――劳动主义”的“最低纲领”，在这个阶段不是用“集体主义”战胜“个人主义”，而是用“劳动原则”战胜剥削原则，具体说就是消灭地主经济，使土地由地主手里转到劳动人民手里，至于劳动人民占有和使用土地的形式已不是最重要的事。这时并不是社会主义，经过几十年“个体主义”的发展时期，直到“自愿的合作化成为集体化的高级形式”，才可以实现“第二步的最高纲领”，这时“通过集体主义社会在本质上的吸力来逐渐使他们进入社会主义。”这样社会革命党便把旧民粹主义的一次性“社会革命”论变成了由最低纲领和最高纲领分别指导的两次革命：第一次革命打倒地主，第二次革命才是打倒资本家，第一次可以以个体劳动农民为动力，第二次革命才要求他们成为“公社”成员，这样就解决了“个体农民”从革命的对象变成革命的动力这一大难题。<br />
&nbsp; &nbsp;这期间社会革命党最重要的贡献是“土地社会化”理论的提出。他们认为，应该依靠俄国农民的村社生活传统和劳动观念，按照农民习惯上认为“土地是无主的，谁劳动谁就有权使用”的习俗，把俄国现有的土地私有制改造成“社会化”。“土地社会化”的优点在于采用了非集中和个人权利和谐共处的原则，可以避免“国有化”的集中垄断和官僚化。它的操作步骤是：首先是把土地变成不能买卖的全民财产，然后把土地委托给用民主方式组建的村社以及以相同原则结合成的地域联盟管理，由这些机构按照“平均、劳动”的原则在耕种者中平等地分配土地，然后通过劳动合作组织，使农民走上集约化的统一模式。<br />
&nbsp; &nbsp;这个设计方案符合俄国平均主义传统，适应当时反对斯托雷平破坏村社的不公正改革的社会情绪。众所周知，十月革命中实行的土地纲领，就不是列宁在《四月提纲》中提出的“土地国有化”的主张，而是“按照社会革命党所规定的方式”解决了土地问题，用列宁的话说，这个土地纲领“完全是按照社会革命党人的委托书照抄的”。（社会化与国有化最大的区别在于，抵制来自国家的垄断，但是这种模式到了共产党有把握控制整个社会的时候，就发生了质变）。<br />
&nbsp; &nbsp;第三，社会革命党除了在村社、农民土地问题上对民粹主义传统进行修正，同时相应地在其他问题上也使“民粹主义变得欧化了”。改变了传统民粹派过去动辄强调俄国独特性的斯拉夫主义倾向与对“西方社会”的敌视，逐渐向马克思主义靠拢。首先，承认资本主义的具有历史意义的创造性工作并制造了能够改变社会的无产阶级力量。其次，抛弃了反对政治自由、认为它仅仅是资产阶级工具的彻头彻尾的民粹派观点，而把政治自由列为其“最低纲领”政治部分的核心，表示该党要为政治自由而奋斗，他们已经意识到，“如果不把政治要求放在第一位，在实现政治要求之前无论是土地社会化、八小时工作制以及最低纲领中其他经济要求都是不能实现的”。<br />
&nbsp; &nbsp;早期列宁认为，承认资本主义的进步，热心政治自由，为发展资本主义的“最低纲领”而奋斗的社会革命党人，要比一味仇视资本主义、热心反自由的“人民专制”、为恢复“公社世界”而奋斗的旧民粹主义更接近自由主义。但是列宁从这些“自由主义”的变化中看到的不是民粹派的堕落，反而称之为“进步”，称之为对马克思主义的认同，并明确指出这些修正了传统的新民粹派是“革命知识分子”，是“俄国资产阶级民主派的极左翼”。而且列宁后来还特别指出社会革命党中正是以切尔诺夫为代表的中间派最倾向于“遵循马克思主义”，倾向于“从小组习气向政党过渡”，倾向于“以社会民主党的原则为依据”，他甚至认为，“关于这个宗派向社会民主党方向转变的问题，现在只是时间问题”。社会革命党的这些“自我修正”，体现了他们“向社会民主党学习的愿望”，和与时俱进的进步。<br />
&nbsp; &nbsp;总之，社会革命党是民粹主义在20世纪初受文化西方化、政治民主化、经济市场化诸潮流的影响，尤其是受斯托雷平改革中“村社危机”的影响而出现的一种修正模式，由于欧化之风日益浸染了民粹派知识分子，逐渐淡化了其传统色彩罗斯，由于马克思主义和自由主义的临时性理论联盟对民粹主义论战的胜利，使坚持革命立场的民粹主义日益向自由民主主义或社会民主主义靠拢。换句话说，革命民粹派的“复兴”与自我“修正”是新形势下各政党各思潮重新定位中最突出的现象。它表现了两个两位一体的特征：其一是民粹派的社会影响与活动能力大大提高，以及革命性与19世纪末相比明显增强。其二是民粹主义的自由主义化与它的革命化、社会民主主义化并行不悖。因此可以说“社会革命党”标志着革命民粹派主义运动的最高阶段。<br />
&nbsp; &nbsp;十月革命后的民粹主义――“新民粹主义”<br />
&nbsp; &nbsp;十月革命后的新民粹派由两部分人组成。一部分是已经转向自由主义理论的“实践的民粹派”，他们来源于19世纪80――90年代革命民粹主义失败以后，一些放弃“主义”转而从事工作的民粹派，也就是阿波拉莫夫倡导的“小事理论”。这种理论认为既然在政治领域无法有作为，“社会革命”的积极行动目标一时达不到，不如退而求其次，不和沙皇政府来硬的，而是脚踏实地的从事一些实际工作，从“小处着眼”地帮助农民。他们不是通过革命方式、而是通过参加地方自治局的活动，在政府与农民之间架起一道桥梁，关心农民的疾苦、解决农民实际生活问题，比如给农民提供贷款、组织销售农产品、提高农民的知识水平和农业技术、改善农民医疗条件等方式逐渐使农民提高劳动能力，走独立发展的道路。<br />
&nbsp; &nbsp;另一部分是社会革命党的学者成员。十月革命胜利后他们脱离原来的组织为苏维埃政府服务，但是他们的理念和价值认同并没有随之改变，他们认为十月革命的意义只在于从斯托雷平式的暴政下挽救了农村公社，为实现“村社社会主义”创造了条件，而且革命后为了抑制农村自发势力，苏维埃政权在集体化条件成熟之前，采取了“把村社放在第一位”的政策，这也为民粹派所拥护。因此革命后民粹主义思想一度相当活跃，在群众中、学术界乃至党政机构都有影响，当时民粹主义宣传也是合法的。<br />
&nbsp; &nbsp;由这两股力量组成的以拥护苏维埃政权为前提的民粹派就是所谓的“新民粹派”。和此前的民粹主义派别最大的不同在于，十月革命后的“新民粹派”已不是独立的政治派别，也没有独立的组织活动，在政治上是完全依附于苏维埃政府的，它只是一种思想学术流派。<br />
&nbsp; &nbsp;新民粹派出现的社会背景是，十月革命后俄国不仅仍是农民国家，而且由于第一次世界大战、内战、饥荒，大批的工人返回农村，在战前最好时候俄国大型工业的雇佣工人也超不过300万，到内战结束的时候，只有1／3的工人还被雇佣。就是这些人中大部分也由于工厂开工不足而无所事事，成为领取救济金的穷人，只有农民作为一个社会阶层完整的保留下来，而且由于工人返回农村，农民比重大增。另外，革命不仅消灭了地主，打击了富农，使农村中农化，加之对斯托雷平改革的反动，农民又回到古老的村社中。战时共产主义阶段人为抑制商品货币关系，货币的使用量极度萎缩到战前的1／70。俄国从普鲁士道路上退下来，又没有走上美国式的道路，而是回到了35万个村社的自然经济中去了。布尔什维克尽管在理论上不满意村社，当时为了抑制农村的自发势力一直实行“反对独立农庄化”的政策，企图把全国变成全俄规模的重分土地公社。十月革命的胜利一方面是无产阶级、社会主义的胜利，另一方面也标志着与西方资本主义对峙的俄罗斯精神的复兴，这种政治经济氛围都有利于民粹主义的活跃。但是这种状况没有持续几年，1929年党外经济学家比较集中的财政部和农业部开始清洗，1930年，在由新经济政策向斯大林模式的“大转变”和全盘集体化的运动的高潮中，苏联宣布破获了一个“反革命地下党”――“劳动农民党”，新民粹主义者的主要代表人物均在其中，15名学者在没有公开审判的情况下被处以死刑，至此，冠以“民粹主义”称呼的思想流派在俄国绝迹。但是民粹派的政治经济等主张在斯大林模式中得以变本加厉的发扬光大（以“人民”名义的“无产阶级专政”，“统一意志驱动的单一经济体系”，领袖崇拜、铁的纪律、只有一个头脑的政党。查韦斯一语道破：“民粹主义就是社会主义”。）<br />
的政治苦役犯与流放者中，该党成员占了大部分。<br />
&nbsp; &nbsp;这些“前社会革命党”的“新民粹派”学者之间差异很大，其主张也是五花八门，最典型的大约有这么几种类型：<br />
&nbsp; &nbsp;一、组织――生产学派。这一派在新民粹主义者中人数最多，其代表人物是新经济政策时期最著名的农业经济学家――恰亚诺夫。恰亚诺夫认为村社农民是俄国这类不发达社会的希望所在，是社会发展与进步的主要动力，任何一种可取的发展计划都不能违背他们的利益，否则农民革命将粉碎这类计划，也就是说像俄国这样的国家，无论革命还是建设，都既不能脱离农民又不能不改造农民。恰亚诺夫学说中最引人注目的理论创新是农民经济运行中的“微观理论”与“宏观理论”。<br />
&nbsp; &nbsp;所谓“微观理论”指单个农民经济运行规律、即农民家庭内劳动与资源分配的经济逻辑。恰亚诺夫认为，农民经济中没有劳动力市场，不知道平均利润率观念，由于农业生产有季节性，农民所拥有的资本就是可观的无用时间。从这种“无用时间”中产生了农民经济的另一个规律，即劳动无限密集化的趋势，在生计的压力下，农民家庭会在同样的单位面积上投入越来越多的劳动力，以致造成劳动效益下降，即所谓的“边际报酬递减”，革命前俄国农业中这一趋势十分明显。“无用时间”与边际效益递减“一方面被用以证明小农的贫困是这种经济内在规律所决定的，（言外之意是不应归罪于资本主义或殖民化）另一方面说明农民经济有极强的适应能力，在劳动不断密集的条件下，对小农家庭来说不存在过剩的劳动力，而且资本主义大农场难以用其生产率的优势排挤掉这些小农”，进而又为“小农经济稳固论”找到根据。恰亚诺夫反对小农经济经不起风吹雨打的说法，他认为非资本主义的小农将按照自己的经济逻辑进化下去，发展为一种非资本主义也非社会主义的“理想”的农民经济。<br />
&nbsp; &nbsp;恰亚诺夫学说中影响最大的还是他的“宏观理论”，其核心就是农户人口结构“周期性变动”说。他认为革命后村社并不存在阶级分化，贫富差别是由于“人口结构的周期性波动”造成的，即劳动力的多寡和赡养人口的比例在家庭中的变动导致一个家庭贫而富、富而贫的周期循环。<br />
&nbsp; &nbsp;恰亚诺夫还认为，小农经济在私有制下不能巩固，只有在村社基础上发展合作社，利用村社传统发展小农之间的合作，可以使农业生产集中化。谁都知道俄国的集体耕种不是有了集体农庄、国营农场以后才有的，而是来自村社传统。但是这个过程的完善与提高是不能由农民自己来完成的，因为农民落后，是“黑暗愚昧的群众”，必须要有科学家的布道和灌输，这些科学家不仅要在技术上帮助农民提高生产，而且要组织农民进行社会变革，“组织――生产学派”由此得名。<br />
&nbsp; &nbsp;二、康德拉季耶夫的行情理论与农村改造论。康德拉季耶夫是著名的“合法马克思主义”的代表人物杜冈－巴拉诺夫斯基的学生，二月革命后曾是临时政府粮食部部长助理。十月革命后两度入狱，后进入财政人民委员部，参与了一系列重大财政经济问题的决策，并起草了第一个五年计划的农业部分，他领导的行情研究所集中了一批当时最有才干的党外经济学家，在新经济政策时期发挥了重要的作用。<br />
&nbsp; &nbsp;康德拉季耶夫主要从事微观经济研究，他最著名的论断是“长波理论”。他在分析了主要资本主义国家大量统计数据以后得出结论，在发达商品经济中存在着平均为期54年的周期波动，经济发展的兴衰与其运行阶段有密切的关联。他认为苏维埃经济不能不受国际经济形势制约，在长波中的衰落期的情况下，苏联的发展不可能很快，因此他反对不现实的“高速度”。该理论使他在西方知名度很高，以至于为研究他的这一理论形成一个学派。后来的两次世界大战期间景气短暂、危机频繁的事实，尤其是1929年起连续数年的“大萧条”的事实似乎也证明了他的语言，也极大地增强了“长波理论”的吸引力。<br />
&nbsp; &nbsp;然而当时在国内他并不是由于“长波理论”而知名，作为“新民粹主义者”康德拉季耶夫与一般民粹主义者一样，把“非资本主义”的、“劳动类型”的农民视为苏联农业的基石，但是与恰亚诺夫的“生产――组织学派”不同，康德拉季耶夫十分强调当时村社份地农业的弊病，对其发展潜力十分悲观，他也不像恰亚诺夫那样否认农村分化，在他看来市场经济下一定程度的分化比宗法式的平均好，应该支持“强有力的农民”脱颖而出，他要求苏维埃政府放宽对独立农民的限制，认为独立农民是俄国农业生产率和商品率提高的希望所在，他甚至认为斯托雷平改革后农业效率提高就是“独立农场”功劳。他在为国家计委制定的15年远景规划中提出，在苏联第三个五年计划结束时“独立农民要比村社占有优势”，到1941年这种“效率高的劳动农户”应不少于3500万人。可见在农村发展道路的整体观点上，康德拉季耶夫的思想与传统民粹主义的村社社会主义观点已有很大的差异，他的“重农”倾向与其说是民粹主义的，不如说是自由主义的魁奈式的重农学派。<br />
&nbsp; &nbsp;用统计方法研究市场动态及其规律是康德拉季耶夫的专长，他在国内热心用这种方法向农民提供市场信息，他所领导的“行情研究所”发行的“行情问题”，被称为“农民索引”，这份杂志收集了各地农产品成本、价格、市场供求变化等方面的统计信息定期发表，这对于指导新经济政策下的个体农民适应市场需求、搞活经济有很大的意义，但是随着“大转变”的来临，以市场指导农民生产的做法自然与斯大林模式的高度集中的计划体制冲突，1928年行情研究所被解散。<br />
&nbsp; &nbsp;三、尤罗夫斯基的“商品社会主义经济理论”。尤罗夫斯基是这三个思想流派中距离民粹主义最远距离马克思主义最近的一支，把他归于“新民粹派”只是由于十月革命前他与上述二人一样，同是社会革命党的成员。与恰亚诺夫、康德拉季耶夫在专业背景上也不同的是，他是货币金融学家而不是农业经济学家，并且与党外经济学家大都致力于应用经济学不同的是，他对通常只在党内展开讨论的社会主义经济的基本理论问题有独到的建树。十月革命后尤罗夫斯基脱离社会革命党积极为苏维埃政府工作，新经济政策时期他是财政部部务委员，国家计委主席团委员，是革命后奠定新的货币金融体系的主要设计者之一。他重视市场经济下的价值规律，主张国家计划与市场机制的协调，在这批“新民粹派”当中，他受马克思主义的影响最深，尤罗夫斯基是第一个明确提出社会主义经济是一种商品经济，价值规律在社会主义经济制度下仍然起调节作用的人。<br />
&nbsp; &nbsp;新经济政策是以允许市场经济发展为特征的，但是由于这个转变最初是作为一种迫不得已的“退却”而实行的，具有明显的被动性。当时苏联还来不及理顺货币金融系统，市场货币关系十分混乱，于是从1922――1924年苏联进行了多次币制改革，建立了新的货币体系。尤罗夫斯基是这一系列改革的设计者，通过改革他看到正常的商品货币关系对苏维埃经济的成败具有决定性的意义，他认为“商品货币规律”即使在战时共产主义条件下都在起作用，在新经济政策中就更不在话下，于是提出了“公有制＋商品交换＝苏维埃经济”的著名公式。<br />
&nbsp; &nbsp;他说，社会主义经济本身就是一种“商品货币经济的特殊形式”，建设社会主义的过程就是完善“社会主义市场经济”的过程，（这是我们直到1982年才提出来的）资本主义商品经济并不是历史上最后一种商品经济，在社会主义经济中“市场作为标准和调节者”被保留着，而价值规律“作为经济中的基本因素”仍然在起作用，计划可以影响市场，但不能代替市场，苏维埃的垄断同一切垄断一样，同样必须受经济规律制约。这些现在社会主义经济学界共识的理论，在56年前是具有开创性的意义的。<br />
&nbsp; &nbsp;其实，这些“新民粹派”是在20年代“这样试试，那样试试”的探索社会主义的发展道路，都是在承认苏维埃政权的前提下以“发展、人道、减少弯路”作为宗旨，遗憾的是，这种可贵的探索付出了血的代价。<br />
