													<h2>王岐山的政治婚姻 他的故事外界知多少?</h2>
						
						
																											<div id="postmessage_817761" class="t_msgfont">(明镜网 杨青溪) 王岐山从西延安一名普通下乡知青成长为泱泱大国最高行政当局的核心决策者之一。他有众多的特殊称谓，每个称谓背后都是一个长长的故事，每个故事都有近乎完美的伏笔，每个伏笔都会在不久的将来兑现。<br />
&nbsp; &nbsp; 他被视为“太子党”，也被称成“女婿党”。但纵观中国政坛，无论是父与子，还是翁与婿，能够相继当上副总理的只有姚依林和王岐山。<br />
&nbsp; &nbsp;&nbsp;&nbsp;他有望在中共十八大上成为政治局常委、国务院常务副总理，甚至可能挑战李克强的国务院总理席位！ (博讯 boxun.com)<br />
<br />
&nbsp; &nbsp; 但是他的故事，外界真正知道多少?<br />
<br />
延安插队两年，与姚明珊相爱<br />
&nbsp; &nbsp; 1969年初，毕业于北京35中的王岐山，随两万多北京知青来到西，在延安冯庄公社插队落户。与第五代领导人中习近平、李克强等当过知青而且留下不少文字资料相比，王岐山在延安插队落户的这段经历，早已无迹可查。<br />
&nbsp; &nbsp; 不过，在2009年2月22日举行的“北京知青赴延安县冯庄公社40L年联谊会”上，100多名北京知青回忆了在延安冯庄插队的经历。<br />
&nbsp; &nbsp; 在冯庄公社插队的北京知青分别来自北京35中和北京女10中，当天参加联谊会的知青都来自这两所学校，其中包括王岐山在北京35中的同学。<br />
&nbsp; &nbsp; 后来，丹柯在网上发表一篇文章说，这次联谊活动，内容丰富，很有意义。虽然是知青中极平凡的普通百姓，但作为一名知青不后悔，正如在饭桌上大家讲到的：不去不知道，不知道老百姓真正有多苦！管大家都动摇过，甚至滴落过眼泪，但我们经过了这一切，走过来了。对人来说这就是一种收，一种锻炼。<br />
&nbsp; &nbsp; 丹柯还写道：作为一名知青，尤其是冯庄的知青，大家的心中也会有着自豪的一面，在我们之中，在这种锻炼下也必然炼就了一些人，由知青成为有作为的人，如：王岐山、李晓阳、任志强、曾钢、杜建英……<br />
&nbsp; &nbsp; 2006年10月，有一篇《留守北的北京知青》文章介绍了数位已在北扎根的北京知青，其中包括高玉珍，她最大的愿望就是能够回到北京。文章说，30年前，落户西延安的2.6万名北京知识青年开始返城，来自北京市朝阳区北子湾的高玉珍期盼着自己有一天也能回到家乡。但由于种种原因，她的这一愿望始终未能实现。<br />
&nbsp; &nbsp; 高玉珍的愿望最终随着肝癌晚期的到来而烟消云散。2006年9月19日，58岁的高玉珍在自己插队的延安市黄陵县店头镇长村的家中与世长辞。她的离去，使至今还留守延安的北京知青人数变为320人。<br />
&nbsp; &nbsp; 文章介绍说，在留守的知青e面，唯有姜作涛一口纯正的北话，“你到南桥山上，一问北京学生住哪e，大家都知道。”北京学生，是当地人对北京知青的称呼，这一称呼一直延续了几十年，另一称呼是北京娃。<br />
&nbsp; &nbsp; 说到过去的岁月，姜作涛显得十分激动。他曾和北京市市长王岐山在一块儿插过队。说到王岐山，姜作涛最大的感慨是“人和人不能比”。<br />
&nbsp; &nbsp; 姜作涛至今保留着插队时的一b木箱子，说曾有外国记者出2000块钱买，他不愿意卖。木箱子上红底黄字的毛主席语录依然十分清晰：“知识青年到农村去，接受贫下中农的再教育，很有必要。”<br />
&nbsp; &nbsp; 那麽，为什麽曾经跟王岐山在一块儿插过队的北京知青姜作涛发出“人和人不能比”的感慨呢？<br />
&nbsp; &nbsp; 21 岁的王岐山到西延安插队当农民时， 姚明珊也是响应号召下乡的北京知青。两人由此谱写的爱情故事未见披露，不过，有分析指出，正是与姚明珊相爱，王岐山才改变了自己命运。<br />
&nbsp; &nbsp; 从1969年初到延安插队，到1971年被抽调到西省博物馆，王岐山只当了两年的知青。而这两年内，他与姚依林的女儿姚明珊相爱了，有分析人士说，王岐山当初能离开农村被调到西省博物馆工作，只有两种可能：一是，他在知青中表现突出；二是，他沾了姚家的光。<br />
&nbsp; &nbsp; 如果王岐山在延安插队时表现突出的话，那麽就应该有相关的文字记载，就像习近平和李克强那样，因插队时出类拔萃的表现而受到嘉奖，然而有关王岐山当知青的文字资料却少得可怜。王岐山沾了姚家的光，可能是北京知青姜作涛后来感慨“人和人不能比”的原因。那麽，王岐山是否真的沾了姚明珊家的光，从此改变了自己的命运呢？<br />
&nbsp; &nbsp; 对王岐山飞黄腾达所倚仗的背景，多年来不少人颇有议论――1986年，年仅38岁的王岐山就攀上正局级高位；21年后，他跨过了进入中国最高权力核心的门槛。这是所有发迹者难免的非议，虽然后来王岐山的能力得到普遍认同。<br />
&nbsp; &nbsp; 王岐山到延安插队两年后，1971年就到西省博物馆工作，这在当时的知青中，可以说是最好的，最令人羡慕的差事。1973年，他又以工农兵学员身分进入西北大学历史系；1976年毕业后，他却很快放弃自己的专业――民国史，转而投入当代宏观经济学的研究；1982年，中共中央书记处农村政策研究室借调走了王岐山，从此他步上从政之路。<br />
&nbsp; &nbsp; 杨韵撰写的《王岐山：中国“拆弹”专家》文章中指出，天上接连掉下“馅饼”来，这与王岐山的妻子、北京下乡知青姚明珊――更准确地说，与姚明珊的父亲，不能说没有关S。<br />
&nbsp; &nbsp; 姚明珊的父亲姚依林，1935年就加入中共，参与领导“一二九”运动。“文革”前他先后在国务院贸易部、商业部、财贸办公室副主任、中央财贸政治部当领导，“文革”中遭受迫害，但是1973年，他被毛泽东和周恩来重新起用，担任对外贸易部第一副部长――正是同一年，王岐山进大学。<br />
&nbsp; &nbsp; 1977年3月，姚依林任国务院财贸领导小组组长。1978年8月任商业部部长、党组书记；中共十一届三中全会以后，任中央副秘书长、中央办公厅主任；1980年当上中央书记处书记――王岐山于这一时期从历史转向当代经济研究。<br />
