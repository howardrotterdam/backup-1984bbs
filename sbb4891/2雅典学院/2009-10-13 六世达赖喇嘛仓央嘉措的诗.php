仓央嘉措，六世达赖喇嘛。他的身世为他的情诗增添了浪漫而神秘的色彩。他的经典的拉萨藏文木刻版《仓央嘉措情歌》，那是为了爱情在青海湖迷失生命的活佛痴情的呼唤。《仓央嘉措情歌》是青藏高原最流行的情歌；《仓央嘉措情歌》是青藏高原最深入人心的民歌。如今他的情诗已被译成二十多种文字，几乎传遍了全世界，新的译作层出不穷。而在民间，有仓央嘉措的情诗达200多首。在今天的西藏，很多人都会唱以他的诗改编的歌，我截取了几首和大家一起分享：<br />
