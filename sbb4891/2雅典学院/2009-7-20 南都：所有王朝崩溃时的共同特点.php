													<h2>南都：所有王朝崩溃时的共同特点</h2>
						
						
																											<div id="postmessage_169364" class="t_msgfont">一个王朝的崩溃是件令人着迷的事情。从宏大的历史上看，似乎都是一股势力猛然崛起，然后经过或多或少的时间，终于变得沛莫能御，然后势如破竹地攻打下京城，然后是扫平全国，或者传檄而定，或者剑及履及，一个崭新的国家就这么诞生。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 这是宏大的史书所构成的一种印象，加之所谓必然性的因素，让我们对于历史产生了一种类似于恢宏壮丽的感觉。实际上，这种印象未必是对的，更多的是那种慢慢焚烧的野火与地火，只是在冲出地面之前，不论是在史书还是民间的传说里，那些都是不值一提的小事，很多人就此忽略了他们的存在。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 太平天国是非常典型的例子之一。在此之前，晚清已经只剩了一个空壳，即使这个空壳也被船坚炮利的洋人轰得千疮百孔。洪秀全不过是个不得志，而且读书也不好的人，即使借着我们这里的造反传统拉起一票人马，在没出广西的时候就已经开始显示出了颓势。不但大肆封王许愿，连后宫都置办整齐了。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 如果深入研究太平天国的话，就会知道其荒唐已经到了极点。所谓的“天朝田亩制度”从来就没有真正实现过，所到之处焚书坑儒不算，还把治下的人民分成男女两营，如果发现有苟合之事，立刻会招致灭顶之灾。与此同时，那些王爷之类的高官则纷纷大建王府，建设自己的小后宫。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 等级制度之森严也是他们的一大特点，甚至已经发挥到了骇人听闻的程度。古人虽然讲究官大一级压死人，但毕竟都是有头有脸的人物，在面子上总还是过得去。而只有那种完全不知道如何用文化维系关系的政权，才能把所有的等级差异如此明显地表现出来。某侯爷的家人触犯了某王爷的威仪，不但自身落得身首分离的下场，连那个侯爷也要负荆请罪，最终被羞辱一顿。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 如此荒唐的政权竟然也席卷了大半个中国，总是有道理在。这个道理不妨从晚清最后几年的一段轶事当中去寻找。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 野史有记，号称自己要“八表经营”的张之洞办了一生的事，虽然始终被李鸿章目为书生，但自然也算是有自己的局面。当临终的时候，末帝溥仪的生父、时任摄政王的载沣虽然与之政见不合，但也是亲临床榻看望之。张之洞毕竟是四朝老臣，临死之时还是念念不忘天下安危，提出要善抚民众。摄政王载沣扬扬得意道：“不怕，有兵在。”张之洞从此再无一语有关国计民生的大计献于摄政王大人之前。在他看来，清朝已经是完了。虽然张之洞没有所谓现代政治思维，但他知道，但凡是统治者勒兵观变的心态一出，这天下从此就算是无可收拾了。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 回到洪杨的太平天国，其实这就是所有给予旧王朝致命打击的关键所在。在尚未知道自己已经病入膏肓的时候，看上去所有正在风光的统治者都是异常强大的，在这之前，他们已经镇压过多少次反抗，并且都轻易得手了。“有兵在”这句话就是支撑所有统治者最厉害的春药，让他误以为自己的位子是很稳固的。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 一次又一次的成功，让他们认为这些东西根本没什么了不起，只要是采取强硬的措施，没有人能扛得住专业的大军。当初皇帝拨内帑200万两、赠“遏必隆”宝刀于赛尚阿之时，估计也是这么想的。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 这就是所有王朝崩溃时的特点，在那之前已经有过多少次不成功的事例，而种种东西都在老百姓的心里埋藏着。这次不能爆发、这里不能爆发，总有一个哪怕是荒唐的机会爆发出来。星火燎原，固然星火是原因，但离离原上草才是星火可以燎原的根本，受害者的范围越来越大、最终参与者越来越多。今天这个火星很容易就灭了，明天有阵微风又吹过来，烧起来的范围大了一些。谁也不知道哪颗火星最终会引燃那场焚天的烈火。<br />
