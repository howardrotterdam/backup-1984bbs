																											<div id="postmessage_842278" class="t_msgfont">贾樟柯的沙龙与另一批年轻人<br />
刘松萝<br />
<br />
昨天，2010年9月23日，我到今日美术馆听了三场文艺沙龙。原本是想听日本设计师原研哉先生和隈研吾先生的沙龙，贾樟柯先生的沙龙夹在中间，所以顺便也听了。<br />
<br />
沙龙是由广西师范大学出版社北京贝贝特出版顾问有限公司主办的，他们还给这次活动冠以“理想国年度文化沙龙2010”。贾樟柯沙龙的全称是：“贾樟柯VS 张铁志：你能独立吗？”。张铁志先生是台湾作家和摇滚评论家，一贯主张独立的精神。我在最后一排，只看到了张铁志，贾樟柯被前面的人挡住了。<br />
<br />
沙龙的主基调是独立。独立的电影，独立的音乐。在讲座上，贾樟柯并不像人们想象的那样狂放。他语气平和，咬字清晰，娓娓道来。贾樟柯说：《在延安文艺座谈会上的讲话》，对中国的文学艺术产生了巨大的影响。以后，文艺工作者在履行社会责任的同时，也失去了独立性，失去了自我。文艺和文艺工作者，变成了政治的道具。改革以后，第五代电影导演崛起，创作了不少先锋性和试验性的作品。现在，第五代导演已经进入了主流，陷入到国家主义和民族主义中了。<br />
<br />
谈到独立，贾樟柯认为，独立是一种精神，是一种态度。独立，是不随着政治和经济的需要走。同时，假如你盯住主流，主流有什么你就反对什么，那也不是独立了。独立，并不排斥与外界接触，不排斥我们的作品进入商业渠道，甚至不排斥与商业合作。独立的电影，也要通过努力进入社会。有些独立电影人以不能进入院线为荣，拒绝推广的工作，这就狭窄了。久而久之，就会进一步边缘化。<br />
<br />
给我留下深刻印象的，是贾樟柯的发问：“独立到底是一种状态，还是一种精神和态度？”他的回答是：独立是一种精神和态度。有了这种精神和态度，体制内的人也能够部分地表现出独立。<br />
<br />
张铁志谈到了商业运作中的问题。在台湾，商业实际上窒息了音乐的精神。谈到台湾，贾樟柯认为，不少台湾的文化人为了进入大陆的市场，改变了自己的观点，去掉了应有的独立性。<br />
<br />
1996年，我在《21世纪与文学》中认为：“创作自由还包括作家的创作活动与商业保持独立。这就需要作家战胜自身的弱点，不为名利所驱使。”贾樟柯的沙龙讲演，让我能够从此关注他的创作活动。</div>

							
							
							
							
