　　《南方都市报》特约时事观察员陈思 2006<br />
　　<br />
　　伊拉克人全都憎恨美国人吗？带着这个疑问，中国《南方都市报》特约时事观察员陈思前往伊拉克，实地采访，竟然了解到：伊拉克人的真实想法，与媒体的宣传南辕北辙。 <br />
　　<br />
　　为什么伊拉克人在电视镜头前大骂美国人？针对这一问题，一位伊拉克人回答说：萨达姆当政时，不准我们用手机，不准我们接触互联网，更不准我们收看外国频道。现在我们甚么都有了。然而，面对采访镜头，还是不得不说，我们讨厌美国人，因为我们的名字和形象都在镜头上，恐怖份子随时可能报复和杀害我们。我们心里清楚，骂美国人，美国人是不会计较的。 <br />
　　<br />
　　在伊拉克，市场上卖得最火的商品，就是卫星接收器（碟形天线外加解码器）。盗版猖獗，最受欢迎的，是美国好莱坞大片。陈思发现，许多伊拉克商人，都在伊拉克各地忙于开厂开店，连包括中国福建人在内的外国商人，也都积极进入伊拉克市场。陈思不解：为什么在战乱中还急于开张做生意？商人们的回答几乎一致：绝大多数伊拉克人都已经准备好迎接新生活了。那些制造混乱的武装分子大多数都是从阿富汗、叙利亚、约旦、埃及等国家跑来的原教旨主义激进份子，他们得不到伊拉克人的支持，不可能坚持太久。 <br />
　　<br />
　　战争中，很多伊拉克人被打死，伊拉克人如何看待？一位毕业于巴格达大学、名叫阿里的青年道：虽说开战以来，伊拉克平民被联军打死打伤的也不少，但是，萨达姆当政时，每天都有很多伊拉克人被萨达姆直接下令杀死；联军围剿武装分子时，如果误伤平民，都在电视新闻中予以报导；然而，萨达姆当政时，多少人不明不白地就死了，又有谁知道呢？ <br />
　　<br />
　　武装分子不断劫持外国人质，并动辄将他们斩首，对伊拉克人有何影响？一位娶了台湾妻子、名叫海达的伊拉克人对陈思说：这些坏事，都是那些外国激进穆斯林分子干的，他们的首要目标就是反美，其他都是次要。为首的叫扎卡维，他长期在阿富汗活动，是本.拉登的追随者。他们在伊拉克为所欲为，实际危害了伊拉克人民。伊拉克人民是善良、宽厚、渴望和平的。前天，我带着我的台湾妻子去出席一个宴会，马上有几个伊拉克人上前向她道歉，请求她的宽恕，因为他们以为她是韩国人。你看，砍头暴行对谁的伤害最重？除了受害者和受害者的家人，就是我们伊拉克人了！陈思恍然道：我独自走在街上时，几乎所有见到我的伊拉克人，都叫我赶紧回去，说别让那些「阿里巴巴」抓了去。他们可能都以为我是韩国人。 <br />
　　<br />
　　海达又说：如果以为激进反美的费卢杰人就是伊拉克人，那对大多数伊拉克人而言，是极不公平的。海达还向陈思分析了他个人理解的「美国战略」：许多反美激进份子分散在世界各地，于是，美国人就开辟了阿富汗和伊拉克战场，并放开所有边境关卡，让所有的人自由出入，结果，全世界的反美武装、恐怖份子都被吸引到伊拉克来了，美国人就可以将他们一举歼灭。这样，美国本土固然平安无事，只是可怜了我们伊拉克人！ <br />
　　<br />
　　陈思最后问：以前，我以为伊拉克人都憎恨美国人，但我来了一个星期，发现多数伊拉克人都认同美国，这是为甚么？海达说：这是很自然的事。萨达姆当政时，伊拉克人都受到严格限制，虽然也有不喜欢萨达姆的，然而，大多数人被萨达姆的宣传蒙蔽了，没有意识到自己应有的自由和权利。后来，美军来了，萨达姆政权被打垮了，借助开放的报纸、电脑网路和卫星电视等，人们意识到过去所受的蒙蔽，恍然大悟，才知道自己过去被骗得那么苦！特别是年轻人，他们将是成长于自由空气中的第一代，他们前程无量。<br />
