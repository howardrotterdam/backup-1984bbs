　　最新一期的《战争史研究》的重头文章有三篇值得推荐（总共只有四篇文章）<br />
　　第一篇是《在大漠那边――近世的外蒙古与战争（上）》<br />
　　从外蒙如何并入中国，到与满清中央政府及西藏的关系，再到外蒙古如何开始倒向沙俄，如何策划独立再到外蒙取消独立，再到“疯男爵”登场，全歼进入外蒙的国民军的历史。<br />
　　内容包含外蒙的风俗民情，满清的统治策略，外蒙与汉人与周边政权的关系以及满清及民国治蒙得失。此文综合涉及到的知识，是我多年注意到的相关书籍都没有的，尤其是关于政治治理及民族关系的方面。<br />
　　第二篇是《元首路德维希・贝克――德国总参谋部军官团谋反史１９１９－１９４５（二）》<br />
　　这是连载的第二篇，本篇继续讲述的是德国总参谋部军官团的在德国特殊历史地位中形成的思维方式、行为方式，以及希特勒上台后，是如何开始从参谋部军官团手中开始夺权的。这些内容往往在各种三帝国或希特勒的传记中都有过介绍，但相比而言，以我个人所见，国内相关同类文章，没有一个真正把问题准确分析清楚的。前段时间，雅典学院有个从阿连得和皮诺切特开始引发的争议问题，即对民选的不好的政党应当采取何种态度？其实，本文连载的第一篇，在讲述总参谋部军官团对纳粹党和共产党这种极端主义政党的困局挑战时，曾出现过的机会以及曾经酝酿的手段，有一个清晰明确的答案。个人也因此认为，任何抽象的政治判断，其实都因具体的对象和情境应有不同的答案。因为对政治问题的抽象，往往会忽略掉对比对象内在的巨大不同。<br />
