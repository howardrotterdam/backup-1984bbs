醉生梦死原文：二十年前，死了一群流氓<br />
<br />
@ 2009-6-9 20:15&nbsp;&nbsp;评论(42)&nbsp;&nbsp;推荐值(325)&nbsp;&nbsp;引用通告&nbsp;&nbsp;分类: 一本正经<br />
<a href="http://www.bullogger.com/blogs/zsms/archives/299402.aspx" target="_blank">http://www.bullogger.com/blogs/zsms/archives/299402.aspx</a><br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;我第一次看到关于535的东西，只有只言片语，说是tank压死了学生，还有一个90-1年，于是我把这个事件当成了遥远的清政府年代的事情，然后还嘲笑写书的，那个年代哪来的tank？之后将这本不严谨的破书丢在了一边。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;我第二次接触到535的东西，也是只有只言片语，但是我明确的知道了三点，一，发生的时间是1990-1年，是gcd执政的年代；二，zf杀人了，这个一直斥责北洋军阀镇压学生运动的zf杀人了，并且是屠杀；三，xs们被杀的原因，是因为他们要求政府实现民主和自由。就为这三点，我和绝大多数的人一样，为那些死去的人哀痛，对政府仇恨，在哀痛与仇恨中泪流满面。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;后来我每一次看到535的资料，都免不了要哀痛一番，流泪一番，兔死狐悲一番，就这样很多年。直到那一天我亲历了上海的反日大游行，开始改变我对535的看法。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;上海反日大游行有这么几个特点。一，游行的绝大部分人其实并不反日，从他们手里拿着的日本数码相机就可以看出来。当然，你非要和我说反日并不代表不用日货，我们反的是这个民族的态度等等一系列的东西，而用日货是出于对科技的喜爱。那好吧，用日货和反日没关系，那么我就不明白，为什么这些用日货的反日青年却不准别人用日货呢？为什么他们要把街上和日本稍微沾点边的东西都砸的稀烂呢？所以第二个特点就是，参加游行的人，基本都有暴力行为，而且这些暴力行为的发生，全部出于强迫别人接受自己的意愿，就算没有进行暴力行为的女生，也一样高声的为这些暴力行为叫好。第三，登鼻子上脸。当时上海的警察在日本大使馆门口拉起了人墙，学生们过不去，我亲眼看见几个学生上去打警察，警察没有还手。还有一些因为打砸抢被警察带上警车的人，结果周围的人一看到，马上冲上警车就把他抢了下来。这些事情能证明他们的勇敢吗？<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;上海的反日游行很明显是在我党的默许下进行的，所以对于打砸抢我党早有准备，当时给警察下令，允许在一定时间和空间范围内打砸抢，过了那个时间段，就要禁止。我正好也看到了，游行结束了，大家在回程的路上，几个学生又去顺手砸商店，警察马上出动制止，直接就和他们说了一句话，“有本事你们再砸一下？”那几个学生就灰头土脸的走了。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;在游行的过程中，有一位老大妈和一个女学生吵了起来，那个女学生好像还是什么领导人物，因为我看见她在指挥给现场的人送雪糕。那个老大妈指责她是在害她的同学，她提醒她近二十年前的那场运动的下场。结果女学生很不耐烦的骂老大妈，“你懂什么？什么都不懂你在这里胡说八道什么？”<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;我不得不承认，是老大妈的话，让我一下子把这场运动和近二十年前的那场运动联系了起来。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;从此后，我在看到535的资料，我总想知道，除了那几个xs领袖之外，其他的人干了什么？而他们的想法又是什么？那几个xs领袖真能代表他们？如果能，为什么会发生柴---玲和王丹(貌似是另一人)被绑架的事情？他们走向广场，真的和那些xs领袖一样，是为了民主事业？（我们假设学生领袖是为了民主事业）我无法找到答案，因为无论是gcd的，还是民运人士的，他们谁的话里都没有个体的存在，都在拼命的叫嚣自己是真理的化身，自己才是受害者。于是我能做的，就是在找到答案以前，不再为535流泪。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;后来我看了一些展现那个年代的电影，不是描写民运的，但是多多少少会表现一下，比如《颐和园》。我在《颐和园》里看到这么一幕，xs去广场游行，一个刚和女朋友吵完架的傻逼，看到那么多人去广场，他问发生什么事了？那个拉他的人说，不知道，很多都去广场了，咱们也去吧。他闲着没事，就去了。还有几个女生，问有车没？当时正好有，于是说，有车就去。后来展现了几次游行，其中打砸一些地方，没人管，还被奉为英雄。然后就是那天晚上，他们几个正在街上走，迎面来了几个解放军，二话不说，他们拿着砖头就朝解放军丢，然后解放军就开枪了，结果他们撒丫子跑的比谁都快。（我不敢保证这全是《颐和园》的内容，也许是几个电影记混的，但是表述的内容不会有偏差）<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;这个情节让我想起了一个新闻。有一天警察发现街上几十辆夏利车浩浩荡荡的往一个方向去了，而车上坐的人，一看就不像好人，于是马上调动警力跟踪车队。后来发现，他么果然是去打群架。后来警察把他们带回警局，一问，很多都不知道为什么去，就是说路上有个认识的人叫他一起走，他一看这么多人，又没事就去了。有知道要去打架的，但是却不知道为什么打，和谁打，帮谁打，也是一看很多人都去了，自己闲着没事，而且这么多人，估计吃不了亏，只能打人占便宜，于是也兴冲冲地去了。这个我在《颐和园》上看到的535片段多么相像啊！<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;所以，我可不可以这样理解，其实所谓的535运动，都是因为死难才被赋予了神圣，其实，和他妈流氓欺负人没什么区别。gcd的一再忍让，只是让他们觉得欺负gcd不会有什么危险，于是抓住这个机会，在别人面前展示一下自己英雄的身姿，好好的过一把投机的瘾。他们心中没有民主也没有自由，有的只是发泄与犯罪，干一下平常自己不敢干的事情。就像我那些参与反日游行的同学，他们回来，除了讨论谁砸了什么东西以外，我并没有听到关于这场游行的其他内容。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;民主自由都是画皮，耍流氓才是实质。当然，由于披上了民主自由的画皮，很多脑残的傻逼也真的以为自己耍流氓耍出高尚来了，每年一到535就哭哭啼啼，纪念一下自己流血的青春，顺便鄙视一下现在的大学生。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;今天的大学生真的不如二十年前的？从上海反日大游行来看，我没觉得有什么不同。不仅与二十年前没什么不同，就是与四十年前，又有什么不同呢？<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;1966年，伟大的学生们，而且主要是清华的学生们，在她们的密谋下，终于酝酿成了伟大的“文化大革命”。不要和我说那是毛泽东干的，没有这帮学生，毛泽东一个人和谁玩？<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;1990-1年，伟大的xs们，而且主要是北大的xs们，在他们的密谋下，终于酝酿成了伟大的90-1x潮。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;2005年，伟大的年轻人们，但是仍然以学生为主，在他们看似密谋，其实是我D默许的情况下，成就了伟大的反日游行。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 结果是显而易见的，文革和反日游行都是标准的流氓运动，那么为什么单单这个夹在中间的x潮就成了正义的运动呢？难道有谁要告诉我，这些个流氓们，恰恰在那一年拥有了极高的素质？有了文化？有了思想？有了理想？并且愿意为理想献身？我们伟大的学生们恰巧在那一年正好改邪归正不耍流氓了？<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;江山易改，本性难移。虽然我目前没有足够的证据去证明当年的x生或是工人是如何的不要连耍流氓的，但是我相信一点，这是一个具有极深的流氓传统的国家，举国的教育体制就是在教育流氓，所以我无法说服自己90-1年的他们不是流氓。无法说服自己他们不是为了争夺对女人耍流氓的权力才大打出手的。有个侧面可以证明一二。运动被镇压后，新闻联播报道，某个菜场周围的居民总算能进菜场买到菜了，而之前，学生们堵着菜场门口不让居民买菜，逼着居民参加运动。gcd惯于胡说八道，但是我们也不得不承认，从那个学生单枪匹马挡坦克的事件上，我们也确实可以看到gcd所说的隐忍。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;我不否认那个年代有英雄，我不否认那些死难者里有很多英雄，但是再多，相对于人群来说，都是少数的。当年的英雄，他们面对黑洞洞的枪口依然抬着高昂的头，今天的英雄，面对着不公的社会，冒着危险发出自己的声音。当年的流氓，在没有枪口的时候，表现的比英雄还英雄，当听到屠杀，马上抱头鼠窜、不知所踪；今天的流氓，在网络上匿名叫嚣，“用别人的鸡巴装硬”，一听说要实名制，马上不敢再说话。英雄永远都是一样的，而流氓各有各的不同。因为流氓是踩在安全范围的边线上表演正义和假装英雄的，当安全范围有变化，他们的行为自然也有变化。二十年前，上街游行的危险都没今天在网络上说话俄危险大，所以这些老流氓们，有什么资格嘲笑今天的小流氓呢？<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;那些每年都要把535当儿童节过的老流氓们，在你们每次回忆自己虚幻的流血青春的时候，请你们想想，人家轮zi功也被镇压了，为什么人家就余波不断呢？而你们在被镇压之后，第二天就销声匿迹，一个星期后好像就没有过535运动一样。为什么差别这么大？因为别管正确与否，人家心里有个信仰，真正是为了这个信仰在做事，而你们呢？嘴里说着民主自由，心里想着金钱和女人的屁股，一帮乌合之众，如何能不迅速烟消云散？<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;综上所述，其实他妈535运动就是个流氓运动，打着民主自由旗号的流氓运动，就犹如今天的流氓们依然在干的一样。中国就是这么一个奇怪的社会，扭曲变形，真正的英雄常被当作流氓，比如李鸿章；而流氓因为会表演，却常常被当作英雄，比如二十年前死掉的绝大部分人，比如tiananmen母亲的儿子，那个傻逼不就是去看热闹被打死了吗？<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;如今纪念流氓已经成了一项时髦的运动，每年都要搞一下。就犹如饭岛爱死了，认识不认识的先纪念一下再说，搞得好像他老婆死了一样。纪念只是为了赶时髦，而赶时髦没有任何门槛，哪怕你只听过“535”这个词，也可以开口说几句沉痛哀悼，所以小学生一算八乘八，都是在纪念。所以每年一到这个时候，我总是知道夏天来了，因为苍蝇又多了。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;PS：我曾经表达过，既然民运人士的目标不过也会死为了毒豺，那么我们为什么要舍弃GCD而支持他们呢？核桃质问过我，难道不能在他们相斗的时候取得一个平衡向民主靠拢？（大意）我当时确实回答不上来，因为我认为自己确实错了，但是还是嘴硬，自己说的什么也忘了。我今天觉得可以回答上来，而且认为我当初的判断没错，因为他们都是流氓。流氓是什么？一群毫无道理可讲的生物。比如一个村子，有两个流氓，他们有一天为了一个女人打起来了。我们能趁机改变流氓或者消灭流氓吗？显然不能，因为流氓终归是为了要得到那个女人，而你无论怎么改，不让他得到那个女人，他肯定不会善罢甘休（如果他们懂得让步，其中一个也不会死了）。他们不是小孩子，排排坐，分果果。既然他们打架鸡犬不宁，不如支持一个流氓干死另一个流氓，这样大家还安宁点。除非你有个办法，让他们两败俱死。这个，很难，因为你不是流氓，你不会用流氓招数。<br />
