																											<div id="postmessage_451030" class="t_msgfont">　　我来介绍一些佛教的基本观点，基本上可以算作南传上座部佛教那边来的。<br />
　　第一，“无”和“涅”<br />
　　世界的本质是“无”，所谓“四大皆空”之类，论证的人很多，也很容易理解，我就不多说了。<br />
　　那么“涅”是什么呢？很简单，就是“认识”到世界和自我的本质是“无”。当然，这种“认识”指的不是从理性上加以理解，而是说从感性意义上加以接受。为了便于理解，可以用“戒烟”来打比方：<br />
　　你了解了吸烟有害健康，这是不够的；了解了以后，强忍着不吸烟，也不完美；完美的状态是什么呢？很简单，就是你根本没了烟瘾，看见香烟也没感觉了。<br />
　　“涅”就是这种状态，一个人从感性上接受了世界的本质是“无”以后，也就自然放弃了种种执念（我执），从而达成完美。这时候生活就像玩游戏一样了（而且还不是游戏痴迷者那种玩法），可以轻松地进行了。<br />
　　话说到这儿，肯定有很多需要进一步探讨的地方。比方说：涅是不是等于就是“满不在乎”啊（当然不是）？之类。一次说起来话就太长了。先到这里。<br />
　　另及：大乘佛教有很多胡说八道的东西，把这些概念搞得乌七八糟，所以有“大乘非佛”之说。如果考证历史的话，上座部的经典有很多较早期的，与佛祖的关系是不成问题的；大乘的经典就没那么可靠喽。</div>

							
							
							
							
