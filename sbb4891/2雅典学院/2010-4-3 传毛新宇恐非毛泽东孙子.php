																											<div id="postmessage_556152" class="t_msgfont"><img src="http://news.xinhuanet.com/misc/2005-09/21/xin_0620902211545243238045.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
