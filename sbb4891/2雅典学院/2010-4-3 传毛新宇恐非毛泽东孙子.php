<br />
张文秋一辈子结过多次婚，攀毛泽东这门亲，是她一生孜孜以求的人生目标。一九四九年张文秋把刚满十八岁的大女儿刘文松（别名刘思齐）嫁给毛泽东和杨开慧的长子毛岸英。毛岸英一年后的十月到朝鲜志愿军总部参战，一个月之后就在美军空袭中牺牲。毛泽东把儿子牺牲的消息瞒了思齐两年，最后还是告诉她，收她为干女儿，后来思齐找了一名军人再嫁。<br />
<br />
　　1959年，毛泽东所剩唯一儿子毛岸青从苏联治病回归，在中宣部任俄语翻译。张文秋立刻想到要与毛家再续姻缘，就把21岁的二女儿陈安云，别名张少华介绍给毛岸青。当时少华已有男朋友，两人相差一岁，就是徐海东大将的大儿子徐文伯。张文秋活生生将两人拆散，第二年毛岸青与少华结婚。毛岸青小时母亲杨开慧牺牲，只能跟着哥哥要饭，被警察打得大脑受伤，两人婚后，生活难以和谐。十年之后邵华生下一个儿子，取名毛新宇。是年毛泽东已经七十六周岁，从来没有想看这个孙子一眼，因为毛家都认为他非岸青所出。小时候新宇追着岸青喊爸爸，就连有病的毛岸青都说：“谁是你爸爸！”青年时的毛新宇已经有和毛家一样的胖大身躯，但是知情人都指指点点，毛新宇的胖和毛家人的胖完全不一样：毛家人的面部肉是松驰的，而毛新宇的颊部两个结结实实的大肉团，是向前突出的，这正是徐家的特点。<br />
<br />
　　徐文伯1937年2月生，一九五八年考入北京大学法律系，一九六二年毕业。邵华于一九五九年也考入北京大学中文系，与徐文伯同为校友。其母为了拆散他们的恋爱关系，通过组织硬将邵华转到大连师范学院中文系学习，因为毛岸青一直在大连养病。与毛岸青结婚之后，1961年邵华才重新回到北京大学中文系读书，延至1966年毕业。徐文伯1990-1999年担任文化部副部长，现任全国政协委员。徐文伯的小妹妹徐文慧，退休军医，现任《将军后人合唱团》副政委，她才是“毛氏皇孙”的亲姑姑。</div>

							
							
							
							
