徐文伯：1937年2月生，一九五八年考入北京大学法律系，一九六二年毕业。邵华于一九五九年也考入北京大学中文系，与徐文伯同为校友，其母为了拆散他们的恋爱关系，通过组织硬将邵华转到大连师范学院中文系学习，因为毛岸青一直在大连养病。与毛岸青结婚之后，1961年邵华才重新回到北京大学中文系读书，延至1966年毕业。徐文伯1990――1999年担任文化部副部长。现任全国政协委员。徐文伯的小妹妹徐文慧，退休军医，现任《将军后人合唱团》副政委，她才是“毛氏皇孙”的亲姑姑。</div>

							
							
							
							
