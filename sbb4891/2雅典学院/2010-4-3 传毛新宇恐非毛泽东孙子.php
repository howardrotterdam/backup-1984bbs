<img src="http://www.cycnet.com/cms/2004/netmeeting/photoalive/W020040722352032343190.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
最近毛泽东唯一“嫡孙”，那个站没站样，坐没坐样的中国军队的博士研究员，大校毛新宇，又在两会抛头露面，成为官媒宠物，实在有辱国人视听。<br />
<br />
2008年，毛泽东嫡嗣毛岸青的遗孀邵华病重，赶上人大政协换届，邵华独子毛新宇被胡锦涛聘为全国政协委员，使得毛氏香火在政协代代相传。邵华于2008 年七月病逝，去年三十九岁毛新宇闹出又继承其母“少将”军衔的笑话，后解释为“误传“，不过，升为“少将“，在胡锦涛关照之下，指日可待。去年十二月二十六日，毛泽东冥辰116岁，作为毛泽东“嫡孙”的大校毛新宇，却没有像母亲在世时候一样，能和两个“姑姑”李敏和李纳，一同在《毛主席纪念堂》祭典“爷爷”毛泽东，这条消息绝不是误传，据说两个“姑姑”，还有一个“堂叔”毛远新，都拒绝和他一同祭拜毛泽东。<br />
<br />
毛新宇自生下来，从来没有见过毛泽东，他自己解释：“我爷爷怕江青害我，所以没让我进他家。”江青五十年代患子宫颈癌，去苏联治疗之后开始与毛分居，在晚年要见毛都得经过张玉凤，“害命”一说乃是无稽之谈。实际上，若没有江青，毛新宇的姥姥张文秋，恐怕自己就在丰泽园登堂入室了。张文秋一辈子结过多次婚，攀毛泽东这门亲，是她一生孜孜以求的人生目标。一九四九年张文秋把刚满十八岁的大女儿刘文松（别名刘思齐）嫁给毛泽东和杨开慧的长子毛岸英。毛岸英一年后的十月到朝鲜志愿军总部参战，一个月之后就在美军空袭中牺牲。毛泽东把儿子牺牲的消息瞒了思齐两年，最后还是告诉她，收她为干女儿，后来思齐找了一名军人再嫁。<br />
<br />
1959年，毛泽东所剩唯一儿子毛岸青从苏联治病回归，在中宣部任俄语翻译。张文秋立刻想到要与毛家再续姻缘，就把21 岁的二女儿陈安云，别名张邵华介绍给毛岸青。当时邵华已有男朋友，两人相差一岁，就是徐海东大将的大儿子徐文伯。张文秋活生生将两人拆散，第二年毛岸青与少华结婚。毛岸青小时母亲杨开慧牺牲，只能跟着哥哥要饭，被警察打得大脑受伤，两人婚后，生活难以和谐，十年之后邵华生下一个儿子，取名毛新宇。是年毛泽东已经七十六周岁，从来没有想看这个孙子一眼。因为毛家都认为他非岸青所出。小时候新宇追着岸青喊爸爸，就连有病的毛岸青都说：“谁是你爸爸！”青年时的毛新宇已经有和毛家一样的胖大身躯，但是知情人都指指点点，毛新宇的胖和毛家人的胖完全不一样，毛家人的面部肉是松驰的，而毛新宇的颊部两个结结实实的大******，是向前突出的，这正是徐家的特点。<br />
