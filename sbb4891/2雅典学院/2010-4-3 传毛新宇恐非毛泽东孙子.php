zt&nbsp;&nbsp;回忆我的同学毛新宇<br />
不是一般的胖，八十年代大家都没怎么见过世面，没吃过麦当劳也还没领教过美国肉山式的胖子，所以那时候毛新宇的体形在学校里还是相当惊人，当时常用的客气说法是做裤子费布，就像说脸大的女生费雪花膏。 <br />
<br />
人胖点儿在中国不见得是缺点，至少美国人这么认为。几年前总统大选，好像是《新闻周刊》一篇文章讲选举中非政治因素的重要性说总统候选人千万不能胖因为美国文化把瘦高和诚实等同，作为对比那篇文章又说东方文化正好相反，胖在中国是诚实可靠的象征。要是按这么推理，从我们新中国成立到现在的几代领导人的体重的升降变化就可以大概推测出我们“全盘西化”或者说是“与国际接轨”的程度――领袖是胖子的时代我们往东；瘦子掌了权我们往西。总的趋势是越变越瘦了，比如朱F基比请勿讨论政治话题瘦比江核心瘦大家都比毛主席瘦，当然，不可避免地中间也发生过一些瘦得不合时宜的情况，比如少奇还有耀邦同志，不过他们都及时地离开了领导岗位，从而维护了这一“体重政治学”定理的正确性。作为一个右倾机会主义资产阶级自由化分子，我每次看见温总理竟然这么瘦就觉得中国大有希望。 <br />
<br />
话扯远了，我是想说人胖点儿没事儿，不幸的是毛新宇还是个傻子――注意这不是一个爱称，也没有贬义，只是北京方言用来描述生活中智障人士的通俗说法。 <br />
<br />
毛新宇高中住校，我们班的范之豪是他室友，好多段子都是范说的，你要是能找到他估计能写一本《毛新宇秘闻实录》了。我们平常看见他也就是白天上课还有课间，不过这两个概念对于毛来说好像直到高中毕业也没彻底区分清楚，他经常上着课突然就站起来走出去，老师也从来不管。每天在操场上上体育的应该都能看见，他总是在校园里走来走去，念念有词地拿着一个地上捡的树枝比划。 <br />
<br />
毛新宇高一刚来的时候到小卖部买东西不知道需要交钱，各取所需，感觉像是刚从共产主义社会发配过来的。关于他的身世一开始大家还颇有好奇心，一阵子之后新鲜劲儿过了也就没人当回事儿了――除了他自己。比如上课，如果那堂课讲的是毛主席诗词或者是《反对党八股》之类的跟drhsr沾边的课文，他就会一反常态在教室里坐上一整节课，而且从始至终带着一脸的光荣就像这堂课是他的颁奖仪式。课下他兴致来了喜欢以“我爷爷”的名义给同学们封官，都是一些古代朝廷里的官位夹杂一些《西游记》人物，应该说历史是毛最拿手的科目，经常能考五六十分，其他科目一概不及格或者干脆不考。据说当时在初中的孔冬梅跟毛新宇正相反，特别不爱听人提drhsr。要是为煽情，我好像有必要说现在想起来他那种想不断提醒你他的身世的着急劲儿其实有种傻子特有的简单和诚恳――可惜当年我们还没有学会这个套路，中学生对班里一个性格孤僻举止怪诞的胖子会是什么样的态度可想而知。 <br />
<br />
葛优回忆他的发家史的时候讲过一个故事，说他刚出道那会儿在一个小话剧团当演员演一个县长怎么着都觉得不像，特苦恼，后来一个老师跟他说其实这不赖你，一个人在台上看着像不像县长全靠周围演员的表演，要是他们不配合，你越演得到家就越别扭。谁要是觉得这个说法说服力不够，可以去找一张金正日视察基层的照片来琢磨琢磨。毛新宇没有小金命好，常有无聊的低年级的小孩儿课间围上来，说：毛新宇，给我们封个官儿吧~，毛一开口那帮孩子就哄笑。上课铃响大家满意地各自散开剩他一个人在操场上，那个场面中的残忍让人想起某一类鲁迅爱写的课文。 <br />
<br />
北大附中的三年还是给过毛一点温情的。范之豪说毛新宇饭量大可他妈要求学校食堂对他的伙食严格定量，饿了同宿舍的同学也不许给他东西吃。学校的一个门房从前当过兵对毛主席有感情，毛新宇总是在晚上跑到西门的小平房里吃看门老头给他买的蛋糕。 <br />
<br />
记得毛还闪光过一次。有一回《北京青年报》上赫然出现了一篇署名“新宇”的散文，在那之前不记得是什么纪念日毛跟着一代表团去了朝鲜给毛岸英扫墓，那篇文章大概就是他在朝鲜的见闻，其中当时被同学们广为传颂的一句话是：“我怎么也不能相信大伯就是眼前墓碑上这三个冰冷的字”――大家怎么也不能相信“新宇”就是面前这一位目光游移拿着树棍挥舞着的同学。让我对他的态度有所改变的倒是范之豪提起的一件小事，他说后来毛新宇一度在宿舍里偷偷喝减肥茶，被他看见了还不好意思地想藏起来。我想也许毛没有我们想的那么傻，after all. <br />
<br />
中学毕业毛新宇去了人大，据说他妈先到北大去游说过，招生办公室的头儿跟她好听的说了一大通最后表态说：欢迎毛新宇同学“报考”北京大学。 <br />
<br />
上大学以后的故事就都是道听途说了，比较可靠的一个是班里一个也上了人大的女生讲的。她说大一那年毛新宇看上了一个她们宿舍的同学，经常到宿舍来找（八九年夏天以前北京大部分高校的女生楼男生是可以自由进出的），通常一有人报信说毛新宇上楼了那个被追的女生要是在宿舍就赶紧到别的屋躲起来，问题是毛太执着，经常会表示要坐在房间里等她回来，于是她们想了一辙：一个人假装出去打水，门口转一圈回来说：“哎，我刚才在哪儿哪儿哪儿看见那谁谁了~”毛新宇一听就坐不住了，立刻抬屁股走人。据说这个拙劣的小品竟然屡试不爽。 <br />
<br />
后来我再没当面见过毛新宇，不过隔长不短的会在各色媒体里看到这个名字。令人替他高兴的是有了职业演员们的配合，他喜爱的“我爷爷的孙子”这一角色演起来比从前轻松多了，而且有越来越像那么回事儿的趋势。正如英国报纸离不开他们的女王，我们的新闻事业也离不开毛主席他老人家，他们有他们的两个小王子，呼而嘿呦我们有我们的一个毛新宇博士。千万不要低估我国市场经济中对“毛”字的内需，既然清朝皇帝一个冷门亲戚的野史都能拍成轰动全国的电视连续剧，《“神五”圆了爷爷的飞天梦》这样的报道怎么能不让神州人民激动不已呢？激动之余我常常很感激我们新闻工作者的职业素质，比那帮无聊的英国小报记者简直强太多了。 <br />
<br />
“树欲静而风不止”―― 唯一让人放心不下的是毛新宇整天被媒体娱乐圈的人拉来推去的，万一被市场给腐蚀了失去“伟人之后”的本色那可就真是“亲者痛、仇者快”了。前一段在网上看到一个白岩松访谈毛新宇的视频链接，点过去的时候我还真替他捏了把汗。在他开口说了第一句话之后我放心了――看着他说“爷爷”两个字的样子，我知道这还是我认识的那个毛新宇，就像在上一堂普普通通的毛主席诗词课。<br />
