<br />
2.中国人之奴性有一种令古今中外奴隶制度相形见绌的特色，就是做奴隶的往往并不觉得自己是奴隶，而最为令人啧啧称奇的是，中国人奴隶通常会比其奴隶主更热衷的去捍卫这个奴隶制度。<br />
<br />
3.杀中国人最多的就是中国人自己……几千年来人人为逞一己私欲过皇帝瘾而杀戮不绝；以人肉为粮史不绝书；花费大量精力炮制出旷古绝今的酷刑；强迫妇女缠足令其形同残废达一千年之久……<br />
<br />
4.以儒家垄断中国思想达两千多年；响应儒家的超世俗肤浅教义令中国的宗教发展半死不活两千年……<br />
<br />
5.怕死的奴才不敢反抗暴政，怕死的主子炼丹以希求长生不老……<br />
<br />
6.响应儒家的「不孝有三，无後为大」兼重男轻女思想令中国人口失控和有量无质，进而为求生存严重破坏中国生态；儒家思想也令中国社会走上极端形式主义的歪途。<br />
<br />
7.一个文明该死而不死，不肯适时而死，强行以极昂贵的仪器和活人的鲜血去延长其性命，这就是今天中国问题老大难的一大根源。<br />
<br />
8.这个永远长不大的中国老头子真的只有遇上优质的全面殖民统治才能得救，盲目的爱他只会令他更加狂妄丶堕落。<br />
<br />
9.自汉代起罢黜百家，独尊儒术……将全国人的思想定於一尊已经够糟，更糟的是，是定於一种以唯道德主义丶形式主义丶性别歧视丶人伦差别丶超实用主义和反宗教精神为本丶以繁殖和愚孝为人生至高目标的教条。<br />
<br />
10.中国特别是自独尊儒家厉行思想阉割极权统治以来，其发展日趋乖张两极，凡事不是过犹就是不及，尽管嘴巴上还在吟哦着甚麽中庸之道。<br />
<br />
11.当一个政府的施政可以完全不理民众的反应，可以草菅人命而绝无被弹劾或下台的後顾之忧，再加上执政者毫无宗教伦理道德之制约，就很难期望这个政府会老老实实的关怀民众的福祉，会提升他们等同於奴隶地位的消费力，和设法满足他们作为人的基本需要。<br />
<br />
12.当中国许多农民和奴工还停留在食不果腹丶甚至是衣不蔽体的赤贫状态，中国却要做世界工厂，把精力资源都用来服务其他全世界人，很难不令人有「宁予外族，不予家奴」丶「量中华之物力，结与国之欢心」之恶心。<br />
<br />
13.不仅是被统治的没有兴趣当家作主，中国统治当局也不想民众当家作主，对一切倡议人民当家作主的言丶行赶尽杀绝。<br />
<br />
14.中国本土的统治者，以至普遍民众，都有一种已经绝种於文明社会的思维，就是发展经济或科技不需要同时发展政治或文化，又或者一旦发展政治，就要政治挂帅，经济让路，过犹不及。<br />
<br />
15.中国的发展，向来都是这样的从一个极端走去另一个极端。非此即彼丶顾此亦失彼丶「一心不能二用」丶不是「勤」得灭绝人性，就是「戏」得毫无节制，故「勤」「戏」不能并存。<br />
<br />
16.中国在所谓「中庸之道」的伪装下，一直被当作是客观持平的人类代表。这错得很惨，误人无数。须知中庸之道，就像无数中国古训那样，嘴上说说而已，大家切勿当真，这是中国文化普遍将理想当作现实的现象。<br />
<br />
17.太多人太极端，才要鼓吹中庸；「中庸之道」即使有，也只是用於待人，不是律己。<br />
<br />
18.中国一天没有政治问责制度丶没有起码的丶会理会他人死活的社会伦理道德丶没有监督各方胡天胡帝的言论自由，就休想摆脱九贫一富的主奴式「经济繁荣」。<br />
<br />
19.中国目前的文化，是彻头彻尾的「为求富贵无所不为」的文化……我依然深信中国这样是走邪门，最终并不能提升中国人的精神文明水平，甚至连物质生活水平也只能在一小 撮「人民」里面有实质提升，<br />
<br />
20.整个民族，或当中大部份人都不脚踏实地，不拿出诚意和一点人溺己溺的人道精神来正视人生，而只一味以强凌弱收拾弱者，或虚应故事，或万般狡辩，则这样的民族，从常理推之，从人类历史观之，不可能有光明前途。<br />
<br />
21.请看看那些衣食甚足的中国大款和中国领导人是怎样的一副嘴脸，有没有羞耻之心？原来在中国，一旦有羞耻之心，即使不被投进黑牢，也很难「先富起来」。<br />
<br />
22.欧盟的成立，把欧洲的平均文明水平拉高了。中国的崛起，却把全球的平均文明水平拉低了。<br />
<br />
23.中国人向不同的人说不同话，也就是「见人讲人话丶见鬼讲鬼话」的言不由衷或见风转舵情况，相当普遍，这个行为习惯令人觉得中国人的看法和行为飘忽不定丶难以触摸<br />
<br />
24.中国人若要得到外国人一样水平的物质和精神生活，往往要比外国人付出多倍的血汗，甚至有时即使你愿意付出更多，也可能都得不到<br />
<br />
25.我们中国的祖先特别自先秦以还，这两千年基本上都是胡混过来的，走一步错一步，走一百步就错九十九步。需要认认真真解决的问题，我们的祖先都几乎交了白卷<br />
<br />
26.我们的祖先愚不可及丶也很不负责任的是，两千年来只是终日沉迷於这些恶习，没有严肃的解决中国人的精神需要，即没有为中国人奠定一个宗教信仰的基础。<br />
<br />
27.中国人拜孔子为师，企图以道德取代宗教！结果是，也必然是，宗教半死不活，道德更是劣冠全球。两样都落空。在这一点上，中国共产党和儒家所做的如出一辙，都是企图以人的道德力取代宗教<br />
<br />
28.中国人的确很笨，没有大智慧，只有小聪明丶小权谋。他们硬要舍易取难，水中捞月，企图将中国变成一个不靠宗教力量的道德大国，妄图只用道德文章和政治口号就把中国变成一个「礼义之邦」。<br />
<br />
29.中国人死不悔改，又拒绝优质文明的启迪，结果把自己的道德伦理搞到连禽兽不如。中国人自己残害自己，是自作孽，就由他吧。但随着中国成为世界工厂，中国劣货大量流出国外，对全球民众造成极大残害。<br />
<br />
30.统治者的奴役、压榨、阉割和镇压，中国人每多脸容扭曲，简直是折腾得不似人形。<br />
<br />
31.我多次发现，大概由于所用管养手法的文明水平不同，洋人所养的狗也明显比中国人养的狗活泼和有自信，它们碰面时就高下立见。<br />
<br />
32.中国人的生活几千年来真的是苦闷无聊到无以复加。即使不吸鸦片，他们也会沉迷于这样或那样的无聊恶习，如缠小足炼丹之类，害处随时比吸鸦片更惨烈。<br />
<br />
33.周作人分析国人嗜鸦片主因是「中国人富的闲得无聊，穷的苦得不堪，（俱）以麻醉消遣。」简直令人拍案叫绝！寥寥数字，即描尽国人的颓废精神面貌。<br />
<br />
34.那个骂「鸦片战争」骂得最七情上脸的中国共产党，原来自己在革命圣地延安就是靠种植和贩卖鸦片发财致富起家的，更加荒唐的是，他们的鸦片不是卖给洋人，而是卖给自己的同胞。<br />
<br />
35.只要中国人的苦闷颓废精神状态不改变过来，只要中国人那种不择手段草菅人命的阴险心态不改变过来，中国人还是要靠鸦片过日子的。<br />
<br />
36.在一个有六成五国民希望来生做外国人甚至宁愿做猪也不再做本国人的国家，万马齐，举国若盲，做人那么清醒有什么意义呢？醒着只会感受到更多的痛楚。<br />
<br />
37.中国不但极其腐化、肮脏，中国人甚至可能已冲出亚洲，不再是「东亚病夫」，而是「世界病夫」了。<br />
<br />
38.面对中国千疮百孔的现状，中国人的反应是怎样呢？还有点良心的会说有心无力，绝大多数会是无动于衷，甚或对情况也不清不楚，总之是继续莫谈国事，继续灯红酒绿<br />
<br />
39.中国这样的社会只会慢性瘫痪而难以崩溃的，中国社会有极其深厚的「烂活文化」，人人都是有潜质忍受大苦大难大侮辱的苟活专家<br />
<br />
40.中国官商乘着世界进一步走向经济全球化，就牵着这这十亿只工蚁像海啸一样扫荡全球。<br />
<br />
41.中国特色的共产党控制下的教育重点只是洗脑，而不是培养国民独立思考或监督政府施政。目的主要是方便让民众接收政府的命令，认同当政者的意识形态，纵容或原谅当政者的恶行，概言之，都是为培养奴才而设的「教育」。<br />
<br />
42.如果一个政治制度只是待民众如牛马，视民命如草芥、以剥削奴役草菅人命为己任，担心监狱而不是民房的稳固，那么也休想这些牛马不如的奴才会有公德精神，毕竟，奴才的天职只是依法舐痈吮痔、蝇营狗苟，救国救民的大任，叫奴才太沉重了。<br />
<br />
43.中国政府或国人越来越有一种怪癖，就是只要找到美国任何错失，就以此作为替中国类似错失开脱的理由。<br />
<br />
44.中国这些恶，由於太过荒唐，超乎文明人的想像力，心智正常的人是不容易听信的。这是中国政府或国人在国际形像上一个从来都受到忽视的绝大优势。其次，事后砌辞狡辩、言不由衷，百般文饰、故弄玄虚，也是中国政府跟美苏政府一个极不相同之处。<br />
<br />
45.中国另一个甚为奏效的伎俩是长期呼天抢地、捶胸顿足、翻旧帐、敲竹杠。<br />
<br />
46.国人将鸦片这种直到明朝还属於止痛药的药品当饭吃，纯乃国人独创，「煮土成膏，镶竹成管」，是国人的专利发明<br />
