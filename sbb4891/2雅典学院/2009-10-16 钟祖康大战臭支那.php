<br />
48.我已经活过42个年头，虽然有些人出于误解而羡慕我的生活，但我觉得我至今所作过的事当中令自己感到自豪的并不多，而其中一样最令我引以自豪的，就是我对极具欺骗性的中国政治的认识没有出过大错，自问比起许多名教授、名人优胜得多。<br />
<br />
49.我从没有做过大中国主义者，从没有膜拜过毛泽东，更没有追随过四人帮，没有拥护过中共收回香港，不需要等到八九年六四屠杀才明白到中共的残暴，和第一时间认同台湾的民主化运动，和第一时间响应台湾的政治自决立国进程。<br />
<br />
50.英国人其实是香港华人，或者说，是中国人的文明导师。我也庆幸，我从来没有像绝大部份中国人那样，为了反殖而倒向比殖民地更独裁残暴的中共政权，又或反日反美霸权却拥护杀人更多也从不忏悔道歉的中国暴政。<br />
<br />
51.殖民统治虽坏，但由于中国人的统治更是坏得匪夷所思，所以，基本上任何殖民统治，相对于中国人的统治，都是较可取的。<br />
<br />
52.在人的解放、平等和尊严这些信念底下，什么爱国主义、爱党主义、民族主义都是可弃之而毫不足惜的。当我发现了公义被埋没了，特别是在愚妄的欢呼和掌声之下被埋没了，我就偏要不惜工本，即使押上个人事业前途也要把它暴露出来。<br />
<br />
53.中国奥运金牌阴盛阳衰是因中国女子较能忍受非人地狱训练，即女孩子能吃苦，许多穷家女被迫走上这条血泪之路。而且女运动员服禁药也比男性更能提高体能。<br />
<br />
54.中国人只有小聪明和大权谋，没有智慧，究其因可能是数千年代代相沿的铅中毒而造成智力低下。<br />
<br />
55.国人之所以蠢，不仅是因为中国统治者两千多年来一直奉行如余英时教授所说的「反智论」，而是，中国人很有可能生理上的智力发展存有重大缺陷，以致做出了诸如把女人的天足捆绑了一千年，将止痛鸦片当饭来吃<br />
<br />
56.中国人血铅含量特高，似乎不自今日始。中国文化中有几种国粹，其实令中国人的智力付上了惊人代价，大抵也是太蠢之故。那就是青铜器，炼丹术和铅釉陶瓷。<br />
<br />
57.中国人口之劣质，已不再限於「东亚病夫」（全国乙型肝炎带菌者已超逾一亿两千万！），也在其智力缺陷；「黄祸」的含义已不仅指中国向外倾倒劣质人口，也扩展至倾销劣质害人商品。<br />
<br />
58.要是中国人不把我骂个半死，那就不再是中国人了，他们这样骂，正恰如其分的说明了这些读者依然是“待人以严、律己以宽”的纯种中国人。这些怒气冲冲的中国读者提出的许多问题，根本不值一驳，当中大都是中国人被迫停止了独立思考起码两千年兼资讯封锁下的井蛙之见。<br />
<br />
59.中国从董仲舒独尊儒术，到李后主发明妇女缠足，再到明清抗拒外来优秀文明的启迪，到共产党在欢呼声中君临天下，一切尽是自作孽，祸去祸来因自招，活活瞎折腾两千年。<br />
<br />
60.成龙说他慢慢觉得“中国人需要管的”。这个脑残式发现，也激起许多脑残式的议论，攘臂张目，不可穷诘。其实问题不是管与不管，而是什么该管，什么不该管，和该管的应怎样管。中国一向的问题都是，该管的不管，不该管的却要管，而且是乱管。<br />
<br />
61.政府原该强力管理，以做到“老有所终，壮有所用，幼有所长，鳏寡孤独废疾者，皆有所养”的，却不理民众死活，任其自生自灭；但像宗教这些应尽量任其自由发展，不该多管的，中国政府却管得滴水不漏，还要以无神论者的身份成立宗教局。<br />
<br />
62.我去中国时尽量自携乾粮，宁愿捱饿也不吃贵国食品，但即使不顾死活的吃饱了，上厕所又是头痛问题。不论是吃饭权还是排泄权，中国人都没有办法解决。但他们的监狱却做得挺坚固的。<br />
<br />
63.邓小平不仅常常吐痰，更多次在对方发言中途吐痰，要对方等他吐完为止。<br />
<br />
64.我们大有理由假设，不论邓小平是蓄意还是无法自控，其痰盂及其频频吐痰的行为，曾经在中国政治，起码是中国外交史上产生过一定作用，就是令对手感到厌恶，坐立不安，神不守舍，进而为减轻痛苦欲速战速决而乱章法。<br />
<br />
65.为什麽那麽多中国女性不被卖为奴婢o就只会被溺毙、饿死或被卖到妓寨呢？当中国的圣人、平民、当中甚至包括妇女o已经一人一脚合力把妇女踩成泥后o就对她们说：你已经走投无路了o让我打救你o买你为奴吧。<br />
<br />
66.为甚麽中国那样失败o为甚麽那麽多中国人要沦为丧家之犬o因为组成这个国家的人o大都是忘恩负义、也不会反求诸己之辈o臭罂出臭草是也。<br />
<br />
67.香港人想吃的只是垃圾o吃好东西反而会呕吐的。看来o这在香港的饮食界和政坛都大抵如此。<br />
<br />
68.若中国人肯用花于推广缠足或研究酷刑的十分之一的精力用于解决同胞挨冻的问题o就不会有那麽多中国人要饥寒交迫了。<br />
<br />
69.说中国人「重和谐」是非常误导的o因为中国人一直追求的只是「表面和谐」（superficial harmony）o而不是文明社会所重视的实质和谐。<br />
<br />
70.中国人千万别以为自己醉心的猴子戏可以瞒得过那些洋鬼子。而无数识字的中国人到今天依然以中国人「重和谐」而自豪o令我几乎笑至气绝。<br />
<br />
71.我虽然自问已使出浑身解数写这些文章o深信这是佳构o但由于内容极度离经叛道o我真的没有想过会这样受欢迎。如此极度离经叛道之作o不要说要成为畅销书o根本是找人出版也艰难。<br />
<br />
72.为甚麽面对俄罗斯时o神圣领土却是「可以分割」的呢？是中国领导收了俄罗斯政府甚麽好处费吗？中国统治者敢说「宁愿台湾不长草，也要解放台湾岛」o何以不敢说要解放海参崴或库页岛呢？<br />
<br />
73.中国政府的「自古以来」之论绝不是甚麽神圣原则。不过是用完即弃的劳什子讹词而已。<br />
<br />
74.中国人都很擅于将异族异地殖民，也对以他们看来是高人一等的汉文明取代异族的文明甚感自豪，甚至以提升野蛮人的文明为天职。但当中国遇到在自己之上的文明时，却极端抗拒被人教化、提升。中国拒绝教化、提升的后果，只是伤害了自己。<br />
<br />
75.中国起码胡混了两千多年，到踔幸兑院螅社会发展已经走到穷途末路，整个社会其实已是一具活死人。西方列强的文明对中国来说是求之而不得的补品，所以我们若真的要怪罪于西方列强，那就是这些列强没有以更强硬果断的手法管治中国<br />
<br />
76.崩溃、破产、离婚等「拉倒性概念」都不是着重表面和谐的中国文化所固有的o这些都是外来观念。中国固有的观念是死拖活拉、是藕断丝连、是外强中乾、是好死不如烂活、是金玉其外败絮其中。<br />
<br />
77.耶稣所受的酷刑不容易令一般中国人感到震撼。由于中国人自力开发的酷刑如腰斩、车裂、凌迟、剥皮、活埋、镬烹、锯割、断椎、刖刑、割鼻、灌铅、凿颠、抽胁等等o已臻于酷刑之化境o一般中国人对于皮肉之苦早以「曾经沧海难为水o除却巫山不是云」。<br />
<br />
78.许多中国人本已感到生不如死o单单「活着」本身就是极大酷刑o天天都在受难。所以o中国人对「受难」的标准肯定比一般洋人订得较高。至于耶稣死前所受的羞辱o对于从羞辱中成长在羞辱度过一生的中国人更不算甚麽。<br />
<br />
79.中国人的父母多渴望子女成年后也尽量不要独立o无论在财政上、思想上和个人行动上o中国父母不是希望子女像自己o就是希望子女按着父母的特定意愿如何不像自己o父母总想跟子女永远保留着纠缠不清、互相倚赖o最好是长尊幼卑的关系。<br />
<br />
80.中国长期在良好制度上交白卷o就制度而言中国是非常匮乏的社会o它没有资格不假外求o而是必须假外求o必须从外国输入良好制度o以补中国的不足。<br />
<br />
81.我有时想，要是真的有天堂和地狱这两个地方，说不定那地狱之门砰然打开后，他妈的原来就是中国。</div>

							
							
							
							
