在一楼大厅里，悬挂着印有毛主席穿绿色军装头像的鲜红党旗。正当同志们聚精会神进行交流发言之际，大约在11.10分左右，突然从农家乐上方的黑山谷风景区公路上冲下来70余名全副武装的警察，他们有的手持手枪和微型冲锋枪，有的手持警棍、警具，气势汹汹、凶神恶煞地冲进会场，恶狠狠地咆哮命令尚在一楼的同志们跪下，强行我们的同志双手抱头。冲到二楼的警察粗暴地将跑到二楼的同志们逐一推揉、踢打集中到一楼和室外空地上，强迫下跪、双手抱头，稍有不从、抗议者，警察便凶残的拳打脚踢进行施暴，随后，将一部分同志上铐，连3名70岁以上的老同志也不例外（仅仅只有76岁高龄的宋道同志没有被铐）。大概当时他们带的手铐不够，只铐了大约一半的同志，另一半同志则被警察强行用同志们身上解下的皮带反手捆绑，在这个过程中，我们34名同志没有作任何对抗，因此警察很容易地控制住了局面。<br />
警察控制住局面后，将我们大部分同志集中到室外空地上，不准我们互相交谈，凡是警察看不顺眼的同志，他们就随意动手打骂。我们有一个从河南来的南文涛同志，在警察冲进会场之前，从后门跑了出去，不慎摔伤，造成了左手关节骨折，他被警察带到现场后，有一个身着便衣的警察用手铐狠狠地铐他，南文涛同志仅仅只说了句铐得太紧，申明受伤的手腕剧痛，这个警察就打了他两拳（后面这个家伙又打了我们其他同志）。该警察身高中等偏矮，年龄大约30来岁，长着满脸横肉，留着小平头。打了南文涛同志后，这个野蛮的家伙故意将手铐捏紧，扬长而去。之后，其他同志看到南文涛同志的左手迅速发乌，则大声呼救，经警医检查后，确定了骨折，这才由其他警察将手铐松了一点，随后被送往山下医院处理。<br />
不久，警察在对34名同志的住处进行仔细搜查后，将行李全部集中到室外空地上，由我们的同志逐一辨认、清点。之后，警察扣押了他们认为是“违禁”的物品，包括电脑、U盘、党旗、宣传资料以及一些个人用品，如银行卡之类的东西。并逐一对我们34名同志进行现场审讯，做了笔录，全部过程比处理刑事犯罪有过之而无不及。<br />
部分警察除了随意打骂我们的同志之外，还令人发指的侮辱我们同志的人格。来自上海的齐力同志，硕士学位，大概在清理个人物品的时候，与警察发生了争执，前面提到的那个小平头、满脸横肉的警察，狠狠地抽了齐力同志一耳光，紧接着又上去一个身着便衣、个子偏高、身体结实、比较魁梧的警察，上去恶狠狠地抽了齐力另一侧脸耳光，在我们同志的怒目注视下，这两个没有人性的家伙才悻悻地走到一边去了。<br />
大约在中午1点钟后，为了表达我们的满腔愤怒，全体同志合唱起了《国际歌》，起初这些警察竟然不知所措，当我们唱到一半的时候，他们才如梦初醒，于是就声嘶力竭地咆哮：“不准大声喧哗”、“肃静”。诺大个空地，我们“喧哗”谁了？于是同志们照唱下去，这下子惹急了一些警察，他们在背后对我们的同志拳打脚踢，企图制止我们唱《国际歌》，而我们全体同志坚持唱完了这首无产阶级战歌，当时的场面极其悲壮、动人，狠狠地打击了那些害怕国际歌之徒的嚣张气焰，在《国际歌》声中，他们终于发抖了！不久，我们34名同志在枪口之下，分别被押上了4辆中巴车，被带到了重庆市万盛区市内的一个警察机关。<br />
在这个机关里，我们听到警察人群中一个身着便装、类似领导头目的人，毫不顾忌的当着我们的面，向“参战”警察进行口头表扬：“今天大获全胜，立大功了！”，他指示“参战”警察撤队休息，由国保大队对我们进行审讯。<br />
审讯一直持续到当晚10点许，其间有几个同志被带走，去向不明，其余的同志则以“非法集会”为由，被处以10天行政拘留，陆续被送进“重庆市万盛区行政拘留所、重庆市万盛区强制戒毒所”（这两个部门在同一地点），分别与7、8个囚室的吸毒人员关押在一起。<br />
从行政拘留的第二天起，也就是10月16日，部分同志被提走，没有再返回行政拘留所。在行政拘留期满之前，共有10名同志被提走后去向不明，他们是：魏晋湘、马后之、张小敏（女）、徐建生、邓？（名不详，四川籍）、刘？（名不详，四川籍）、张巨雷、张长生、白万祖、严忠伟。<br />
特别指出的是，当我们34名同志全体被押解到了重庆市万盛区市内的那个警察机关安定后，还是前面提到的那个小平头、满脸横肉的家伙，因为我们一位从广东来的同志辩白了几句，再一次故伎重演，恶狠狠地将其手铐捏紧，施行残酷“合法”的折磨，对这个没有半点人性的家伙，必定清算十恶不赦的罪恶！<br />
在以上整个事件过程中，我们34名同志没有一丁点暴力对抗，没有一个人携带任何“违禁”器具，甚至连一把小刀都没有，完全是手无寸铁。就是在这种情况下，那些丧失了人性的凶残野兽们仍然对我们的一些同志施暴，其罪恶无不令人发指！<br />
自10月19日开始，“涉案”各地国保部门派员陆续来到重庆市万盛区行政拘留所，提讯了他们辖区参加“非法集会”的同志们后，除了10名同志之外，其余22名同志陆续被“涉案”地国保部门带回各自省市，结果不详。行政拘留期满释放的同志仅有2名。云南国保部门在带人时，用手铐反铐住被带走的一个同志。<br />
