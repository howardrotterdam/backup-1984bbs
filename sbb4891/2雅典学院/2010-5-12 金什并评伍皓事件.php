													<h2>金什并评伍皓事件</h2>
						
						
																											<div id="postmessage_610519" class="t_msgfont">4月22日云南省委宣传部副部长伍皓在人民大学演讲，被人扔了一把五角人民币（“五毛”是网络上习见的对支持政府者的蔑称），成为网上（尤其是微博上）轰动一时的事件。半个多月过去了，我想还是对这件事发表一下意见吧。<br />
　　我很佩服伍皓面对搅场不动声色、化闹剧为喜剧的作法。要是我的话，肯定做不来，这倒不是因为我害怕言论自由，而是我的确认为这是对我个人的侮辱，其实这也是绝大多数人遭遇这种事件时自然而然的感觉。伍皓通过长期的训练，成功克制住了个人的情感，这不是一般人在短期内可以做到的。从这个意义上说，伍皓的确已经具备了一定的政治家才干。<br />
　　但是我对伍皓后来和搅局者约定在后海见面的决定不以为然（当然，最后他也爽约了，表面理由是怕被人指责为做秀）。其实我也很佩服他能做出这个约定。要是我的话，肯定从一开始就不会去，原因也还是和个人尊严有关。我认为我没有必要和每一个人见面，因为有些人实在不值得一见，和这样的人见面不仅是浪费时间，也是降低自己的身价。虽然孔夫子有云：“三人行，必有我师焉。择其善者而从之，其不善者而改之。”但是人类的“善”说穿了没几种，“不善”说穿了也没几种，不善者见多了，就知道其“不善”总共也就那么几样，对这几样“不善”都有了认识之后，又有什么必要和新的不善者继续一起“三人行”呢？然而伍皓不在乎这些，这再次说明他的确已经具备了一定的政治家才干。<br />
　　我对他这个约定不以为然的真实原因在于，我认为即便从政治的角度讲，也没必要和扔他钱的那人及其同党见面。<br />
　　网上很容易查到，扔钱者叫王仲夏。网上也很容易查到，王仲夏是辽宁人，生于1984年，人民大学经济学院2003级毕业生。王仲夏的网志也不难搜到，大家尽可以去看看此人是什么货色。我只摘录一段：<br />
<br />
&nbsp; &nbsp; 　　前阵子回辽宁老家，我开车在高速上持续跑出200公里的时速，回到北京已有大概10个工作日了，未见交通处罚纪录，等于我挣了两千块钱（据说我那个超速的级别至少罚款2千块），很好狠爽，还有我们一家三口人的性命，价钱就更不好说了。<br />
<br />
毫无疑问，这又是个游民。不过请恕我用词不雅，我至多是在学术层面上称他为游民。纯个人来看的话，这就是一贱货。和这种贱货见面？那我还不如去死。<br />
　　再来看看后海之约里面想和伍皓见面的三人中的另两个。赵晖是六@@四遗老，虽然他的年龄不算太大，肉体应该还能继续存活几十年，但是他的思想已经定格在了二十一年前，或者说，已经死亡了。他的一切言行都显示，他现在只是被这种二十一年前的简单退化思想操纵的行尸走肉罢了。如果是我，我也不会和行尸走肉见面，何况还是一具动辄披上“莫之许”画皮的行尸走肉？我在网上查到了赵晖的好几个头衔，一会儿在《战略与管理》工作，一会儿在《华夏时报》工作（这两家报刊现在是一家停刊，一家转型），一会儿是“孙冶方经济科学基金会项目总监”，一会儿是“北京莫之许文化传播有限公司总经理”，这种人如果不是游民知识分子，还有谁是呢？<br />
　　另一位苏雨桐是女性，我慕名上网搜了她2007年的照片来看，打扮非常小资，这就是说，不算难看，但也非常平庸。她是环保分子，“自然之友”会员，不过主要是为另一个环保NGO“守望家园”工作。这个NGO重点关注污染问题，这是对的。不过可能正因为真正戳中了中国环保的软肋，所以才常常和真实的腐败和政府不作为打交道，所以其中的人倒向“柿油派”似乎也就自然而然。<br />
　　不过苏小资是先倒向“柿油派”才选择做伪环保分子“曲线议政”，还是做了环保分子才倒向“柿油派”，还需要考证（但是我已经没兴趣了），因为在关注污染的环保人士里面，大多数并没有像她那样，成天在推特上把全中国被捕入狱的“政治异议人士”的人名都堆砌在一起，让不明真相的人以为中国的政治迫害问题很严重――这就好比一个只有100元钱的穷光蛋把仅有的这100元钱兑成了100张1元纸币，然后洒满自家门口，结果让不明真相的人误以为他很有钱似的。<br />
　　一个是无赖得惊天动地的贱货，一个是猥琐的行尸走肉，一个是从事伪环保的小资女，三个人分别代表了大城市里面思想最低级、最野蛮的文化人的一个侧面。伍皓本人是稳健派，立场不左也不右，他要代表政府团结民众，最可行的办法理应是先团结和他一样的中派才对。但是伍皓却对王仲夏、赵晖、苏雨桐这种极端右派抛出橄榄枝，在我看来，实在是太天真了。这三个人怎么可能被你团结过去呢？放着更需要交流的中派民众不管，却试图通过和极端右派的对话来证明自己的温和，被人称为“做秀”，难道不是理所当然、合情合理吗？好在伍皓最后退让了，如果他真的和对方见了面，那我敢肯定，他就算再有政治家的才干，但因为头脑根部有问题，将来如果发达了，恐怕反而要给中国再次――我不想说第一次是什么时候，但是知情的朋友，你们懂的――带来祸害了。</div>

							
							
							
							
