　　“天上人间”夜总会的全称是：北京长青泰餐饮娱乐有限公司“天上人间”夜总会。法人代表林美凤，外资企业。<br />
<br />
　　按照北京工商年检的资料，这家国内驰名的夜总会2001年度净利润仅为4.86万元；2002年度，利润总额为42.76万元；2003年度，竟成为亏损148.13万元的企业。<br />
<br />
　　“天上人间”夜总会还包括北京妇女活动中心的“钻石年代”夜总会和深圳圣廷苑酒店中的“天上人间”夜总会。<br />
<br />
　　覃辉辩称：“有6、7个股东控股‘天上人间’，目前自己只留了极小的一部分股权，已有6年没去过‘天上人间’的办公室，现场也极少去”。实际情况是，除了北京“钻石年代”夜总会因为覃辉赖帐而未能全部买下股权之外，其他两个“天上人间”的全部股份都是覃辉一人的，据说只有一个陈姓股东与其合作过几年，现已完全退股。“天上人间”夜总会是覃辉的“龙翔”之地，是他的印钞机，也是他结交权贵，实施公关的天天必去之地（不在北京时例外）。<br />
<br />
　　覃辉是1995年接手“天上人间”的，他当时在武汉钢铁公司做进口矿石的买卖，第一次被“协助调查”，折戟而归。为了找一个挣钱的买卖，向当时的首都机场管理公司的总经理借款180万美元，并由军队一家贸易公司担保，买下原隶属广泰公司（台湾人）的“天上人间”夜总会。<br />
<br />
　　“天上人间”的初期管理由台湾黑道“四海帮”掌门陈永和（外号“宝哥”）派得力干将帮助打理，要求很严。挑选服务员有如下要求：身高1.70米以上，三围的尺寸是：胸围80公分以上（假胸亦可），腰围60公分以下，臀围75公分左右。但尽管如此苛刻，“天上人间”歌舞厅也没有表现出太多的过人之处，转机出现在 1996年3月份的“两会”期间。<br />
<br />
　　1996年3月，正值中央召开“两会”，“天上人间”夜总会发生了一件大事，事情大到惊动了总书记的程度。<br />
<br />
　　北京市公安局西城分局两位副局长吴长城、崔铁英，以检查为名，便服私访来到“天上人间”，半小时喝了一瓶“皇家礼炮”，不识相的张经理上前要求结账，正在梦境中的两位局长哪能在号称“京城第一选美场”的众小姐面前丢人，眼一瞪：“这酒是假的，结什么账？！”<br />
<br />
　　几句下来，话不投机，吴、崔两位既是主管歌厅的副局长，又都是干部子弟，（其父均为少将老红军，官拜北京军区的副参谋长和北京卫戍区的副司令）当时就破口大骂，拿起酒瓶砸将过去。张经理忙不迭请示正在楼上潇洒的覃辉，覃辉一声令下，亲自动手，众保安一拥而上，三拳两脚就打倒了两位“镇关西”。两位副局长头破血流，其中一位还断了两根肋骨，仓皇中吴副局长拨通“110”，以保卫两会为名，紧急调动了武装特警两个分队，十几分钟内把“天上人间”围了个水泄不通。覃辉一见势头不对，从后门紧急开溜直接跑回“中南海”搬救兵。正当武装特警们实枪荷弹，反过来把众保安打的“头破血流”，一个个像俘虏一样高举双手罚跪在墙边等候处理时，“中南海”的电话已打到北京市公安局前任张局长座机上。据说江总书记口头批示：“什么人敢在两会期间去涉外酒店持枪打群架，严查严办”。<br />
<br />
　诚惶诚恐的张局长从严惩处：崔副局长清除出公安队伍，吴副局长检讨较好，下放至密云县任公安局副局长，改过自新，以观后效。<br />
<br />
<br />
　　这件事影响极大，轰动了整个东南亚，连“美国之音”都做了报道。当时深圳的一个前政府副总理的公子打了前深圳市长的老婆和公安，也闹得不亦乐乎，时称“北覃南刘”。台湾媒体戏称：“太子党内部争高低，中南海淹没警察局”。<br />
<br />
　　类似的打群架，诈场子的事就太多了，但覃辉仗着后台硬，在10年经营中，净赚了2亿多人民币，且在税务部门留下了累计亏损的记录。如果说“天上人间”还亏损，那就是这些亏损都亏到应该去的地方了。<br />
<br />
　　“天上人间”的服务员，小姐不乏高学历，身材姣好，美貌艳丽的少女，这些人中的某位只要被覃辉看中，或被覃辉需要巴结和公关的人士看中了，覃辉手下就会出面游说，在金钱的诱惑下，鲜有不从者。去年，有一位在“天上人间”混迹近十年的小姐，号称“四大名妓”之首的梁海玲遭人盗抢而被杀害，警方竟清理出梁的个人遗产有1000万元之巨。小姐尚且如此，何况老板乎！<br />
<br />
　　有一年，新上任的北京市朝阳区公安局麦子店派出所的所长听说“天上人间”名气大，想“执行公务”在长城饭店路口盘查小姐和服务员，让覃辉拍着桌子赶走了，这位所长直纳闷：何方圣灵，如此神通？<br />
<br />
　　目前整个卓投系企业，实际也就是覃辉资产王国的总资产不过20个亿左右（还不是目前的价值，而是覃辉没有“协助调查”之前的价值）。但这些企业涉及的贷款总额据说在25亿元之上，这里面有没有权力经济和关系经济的影子呢？<br />
<br />
　　据说，覃辉的真正老板是前国家主席的下一代。相信在司法公正越来越公正的现今，那些下决心要让覃辉“协助调查”的检查官们，绝不难查出这里面的幕后真相，随便一个有良知的公民都会问：星美贷款既然符合手续，覃辉何必贿赂上门呢？因为按照现今银监会颁发的规定和制度，如此大的贷款没有总行高达十人以上的风险控制机构认可是根本贷不下来的？覃辉和张恩照之间还有谁的影子？还有谁的手？<br />
<br />
　　有了如此背景、如此后台的覃辉，“行事低调，不张扬”，有例为证：<br />
<br />
　　2004 年8月，挂着中央警卫局特属车牌的一辆奔驰600型轿车来到北京鹏润大厦门口，在一个特殊的停车位处不慎剐碰了一辆挂着中央军委某局车牌的老式皇冠轿车。从皇冠轿车中出来一位老司机，正待指责对方，没曾想从奔驰轿车上跳出一位膀大腰粗的保镖，一拳一脚，老司机仰面朝天倒在地了，顿时围上来一群过路人，纷纷指责那位年轻的保镖。“110”警车按时赶到，可一看两个车牌，当时傻眼了。按北京市公安局规定，这些标志着特殊身份的车牌的轿车是没权干涉的，只好劝开两位司机，好言抚慰。老司机愤然离去，半小时后，一位前共和国元帅的办公室秘书打电话给覃辉的丈母娘家，说明打人者是覃辉的司机，被打者是老帅的司机，让前国家主席家必须处理，否则影响不好。覃辉毫无悔意：“打架是司机之间的事，凭什么让我道歉！”但最终还是骼膊拗不过大腿，拿出5万元摆平了此事。<br />
<br />
　　同年某一天，覃辉开车去“皇城老妈”吃饭，出门时自驾的奔驰600被一辆军车挡着，他顿时火气上涨，猛一踩油门，撞坏了军车。待对方报警叫来交警时，他甩下一句话：“耽误我一分钟知道是多少钱吗？”说完撇下发楞的交警和被撞的军车一走了之。<br />
<br />
　　覃辉在京拥有三处私宅，其中在燕莎商厦后面的一处私宅总资产达2000万元之巨，光装修一个厕所就花了50万元。其父母在儿子的庇荫下，有两处私宅，并有卓京系的飞腾公司的老总李保成亲自打理。<br />
<br />
　　覃辉有“座骑”六辆：美制“悍马”豪华吉普车一辆，德制奔驰600一辆，宝马 750一辆，劳斯莱斯一辆，宾利一辆，其中“宾利”车是当时北京车展中仅有的三辆中最贵的一辆，总价888万元人民币。覃辉买下此车后，在收购著名相声演员姜昆创办的“昆朋网城”时，送给姜昆过瘾，姜昆着实风光了一回。<br />
<br />
　　以上私宅，私车总价值约5000万元，墓地价值200万元，而且奔驰600挂的是中央警卫局的军车牌，宝马750挂的是中央警卫局的地方车牌，据说均是中央警卫局领导亲自特批的。而这两副车牌在黑市的价值是多少呢？一个温州藉的民营企业家曾出资100万元/年购买这两个车牌，覃辉根本不卖。<br />
<br />
　　以上这些资产还不包括覃辉在海外购置的房产和覃辉私人名下的其他资产，已有上亿元之巨了，那么属于公司的资产呢？<br />
<br />
　　媒体均认可的受覃辉实际掌控的内地公司和上市公司有：卓京控股、星美传媒、长丰通信、英斯泰克、友通数字、飞腾制作、鲲鹏网城、华夏文化等。香港上市公司有：星美国际、星美出版、星美广告等，这些令人眼花缭乱的公司总价值20多个亿，但覃辉利用各种手段在银行贷下的款就有多少呢？25个亿左右！<br />
<br />
　　其中建行6个亿，民生银行10亿─11个亿，招行、中信和其他小银行约10个亿。也就是说，覃辉同那几位号称“资本大鳄”的人一样，用国家银行的钱构筑了卓投──星美系的庞大资本帝国。所以，确切地说，覃辉有钱，但覃辉应该属于一家国营大企业的掌门人，并直归国务院大型公委管辖，那样就“名副其实” 了。<br />
<br />
　　说出来人们可能都不信，号称传媒首富的覃辉在业内更是出名的欠款大户。<br />
<br />
　　覃辉的第一笔生意，即运作铁矿石买卖时，就以欠款不还而著称，他不仅不支付正常的航运费，甚至连积欠航运公司的运费都赖着不还，以至于涉足此行不久的卓京商贸就列入了航运公司的黑名单。<br />
<br />
　　“天上人间”是覃辉的发家地，也是业界公认的创利大户，但“天上人间”仍有巨额欠款，其中有近10年的装修尾款，3年以上的货款，1年以上的税款，据说连当初购买“天上人间”的借款都没有还清。<br />
<br />
　　星美传媒收购了姜昆的“昆朋网城”，姜昆一分钱没拿到，仅坐上了“宾利”招摇过市。因股东多次置疑，姜昆一筹莫展，赵本山帮姜昆出主意：“这么好一部车，干脆让我帮你忽悠出两千万来？”玩笑归玩笑，无奈之下姜昆只好把车还给覃辉，可购买股权的现金至今未见着落。<br />
<br />
　　吴征、杨澜夫妇把“阳光卫视”和“现代旌旗”卖给覃辉，覃辉根本不付余下的欠款，几乎月月拖欠员工工资，现今闹到法庭上，覃辉对覃宏说：“两个上市公司都是只亏不赚的公司，他们首先骗了我，凭什么还跟我要钱？打官司就打官司，拖到底就是胜利。”<br />
<br />
　　关键词三 实际掌控人<br />
<br />
　　很多媒体在报道覃辉时，都使用了“实际掌控人”的名衔，为什么呢？<br />
<br />
　　关键词四 协助调查<br />
<br />
　　此次覃辉“协助调查”，一时间在业内掀起轩然大波，似乎出了个大新闻。其实，协助调查已是覃辉的家常便饭了。<br />
<br />
　　1994 年前后，覃辉以卓京商贸的名义，依靠着一家军队的公司开始了当时控制非常严格的铁矿石进口业务，由军队公司开具信用证，前国家主席家乡的大钢铁企业──武汉钢铁公司接收矿石。短短八个月，覃辉就大赚了一笔，而军队某公司和武钢亏损。不久，武钢的原料科长被控受贿，首先交代的就是覃辉。覃辉被拘“协助调查 ”，十五天不到，检察院接到上峰的意见，覃辉无罪释放，武钢的买卖自然终止。<br />
<br />
　　1996 年3月，不甘寂寞的覃辉接手了当时经营并不太好的长城饭店“天上人间”夜总会，首都机场管理公司的老总借给他公款180万美元，军队某公司做了担保。经过暴打公安局长一事，“天上人间”财源滚滚而来，可公款有借无还，机场公司自然追诉担保公司。军队开始调查，覃辉又一次“协助调查”，但由于军不管民，覃辉此次在家协助。不久，军队某公司的老总被撤退休，退到覃辉下属某公司任职，安享晚年。<br />
<br />
　　2002 年，北京朝阳区税务局发现国内外声名显赫的“天上人间”夜总会累计亏损几百万元，开始了税务抽查。可查到“天上人间”，发现这个外资企业的法人林美凤，几年都没来中国一次。再往深里一查，原来实际掌控人覃辉就在眼前，这一次覃辉又开始了“协助调查”。这次“协助调查”同样不了了之了，因为“亏损” 几乎是国内外很多企业偷逃所得税的惯用伎俩之一。而究竟“天上人间”有没有偷逃税款呢？据说几位税检人员在当年圣诞节被邀参加了圣诞抽奖，仅1800平方米的“天上人间”夜总会的包房和迪厅里，众多宾客当晚消费了80万元，每平方米的产值高达450元/天，几个税检人员瞠目结舌，惊诧不已。最后，“天上人间”亏损至今。<br />
<br />
　　相信这一次“协助调查”，覃辉驾轻就熟，因为毕竟出自巴山，发迹于中南海，什么大山大沟、大风大浪没有见过？<br />
<br />
　　关键词五 空心大佬<br />
<br />
　　真正进入卓京系和星美系圈内的人都知道覃辉有个绰号：“空心大佬”。<br />
<br />
　　这个绰号是谁起的呢？那就必须从覃辉的两次艳遇说起。<br />
<br />
　　覃辉原配林菁死后，覃与女儿一同定居加拿大。已至而立之年的覃辉不久就看中了著名化妆品女皇郑明明的掌上千金陈维蕊，陈小姐仪态端庄，郑明明在他们初次相面时，恐怕来自中南海的大少瞧不起自己的女儿，再三说明：“我们蕊蕊可是经过英国良好教育的淑女”。两人相识不久，覃辉即露了本性，他每天混迹于“天上人间”的众佳丽中，不要说投怀送抱的不在少数，就是手下的经理和保安们只要看出覃董的意思，哪一个不是冲在前面为其“保佳护送”呢？有一次，覃的司机兼保镖小海为其挑选了一名大连美女，直送府上。那天原本覃辉一人在家，可以着实亲热一番，没曾想陈维蕊突然从香港飞来，撞个正着。陈维蕊在香港花花世界长大，这类事见得多了，但还从未见过热恋中的男友竟在“热恋”中如此张狂无忌。经过近半年的拍拖，陈维蕊发现覃的许多令人无法容忍的粗鄙，而且真正了解了他的底细，就礼貌地与覃分手了，郑明明问其祥，陈维蕊答道：“京城有‘空心大佬’，覃辉算一个”。<br />
<br />
　　覃辉收购“东方魅力”后，香港艺人曾志伟把号称香港第一美女的李嘉欣介绍给他，覃辉欣喜若狂，拿出了混身解数陪伴她飞拉斯维加斯、赴巴黎，购买成箱成箱的巴黎时装，千金买笑；一掷上百万美元，送李美女生日钻戒；整层包下豪华餐厅，与李嘉欣眉酌眼。但他太低估了港姐冠军，曾被很多阔少、阔佬追逐过的香港第一美女岂是等闲之辈。仅仅又一个半年，覃辉欲见李一面都再无机会。无奈何，覃辉求曾志伟为其说情，曾问李何故不理覃董事长了，李说了一句：“‘空心大佬 ’ 一个，既无才又无财，算个过路朋友吧”！<br />
<br />
　　现今很多媒体出于追逐新闻的需要，千方百计获取覃、李拍拖的花边消息，但圈内人都知道，那是“昨曰黄花”，明天不再了。<br />
<br />
　　关键词六 覃氏战略<br />
<br />
　　不少人关心覃辉的事，都在问？覃辉还有机会吗？<br />
<br />
　　有，一定有。<br />
<br />
　　如前所叙，覃辉的资产王国几乎都是银行的贷款构筑的，银行会允许他猝死吗？不少人知道，除了原中行的王雪冰行长，这次落马的建行行长张恩照之外，跟覃辉来往最密切的是民生银行的行长董文标，同时民生银行的几个分行和支行的行长与覃同样过从甚密。更有人说，由于覃辉资产王国的贷款额太高，范围太广，连前银监会副主任，刚被免职的阎海旺都出面帮其协调各行关系。是企业就要与银行往来，权且把以上交往都看成是正常的，贷款也符合手续，那么越是如此，这钱就越好办。除了张恩照受贿之事已经败露，余下的问题不就是经营不善吗？还得了就还，还不了就以财产抵债，再不行干脆由银行冲坏帐、冲呆帐，在资本市场中长袖善舞的覃辉很懂这个“中国特色”：赚到手是自己的，赔了的钱是银行、股民的，经营者何错之有？<br />
<br />
　　更为重要的是，已经几次从“协助调查”中安然脱身的覃辉，依靠的并不是本身的神通，而是依靠着身后“中南海”里那深不可测的海水，海水里的关系户们，恐怕不会有一个愿意看到覃辉的一朝倾覆。因为他们都明白尽管倾覆后的废墟中将有巨额的人民财产，而为了保护人民的财产尽管他们可以不必像英雄王成一样正气凛然：“向我开炮”，但必竟是需要他们操心的，瓜田李下，三人市虎，更何况覃辉的传媒王国和资产王国中总还是有他们一份心血吧！<br />
<br />
　　现在最要紧的是看覃辉的动作了，尽管又一次“协助调查”，但那毕竟已经翻过一页了，新的文章怎么做？据最接近覃辉的人士透露，覃辉制定的战略是：能拖的拖，能甩的甩，能顶帐的就顶帐，完全没办法的能赖一点儿算一点儿。最后剩下核心的挣钱企业就是胜利。但他面临的最大考验是：既不能再出现新的“协助调查 ” 的问题，也不能“协助”检察机关真的去“调查”，把“中国特色”的真相调查清楚了，那一定会损害中国特色的社会基础和社会关系，这是贾宝玉脖子上的通灵宝玉，对覃辉来说，丢失了它就意味着丢失了性命，还说什么钱呢？ <br />
<br />
　　据中纪委内部人士透露，李培英一进入中纪委视线，中纪委副书记，主抓陈良宇案的何勇，就定下了调子，说这个人关系太广，上上下下认识那么多人，包括中央领导。知道那么多事，一定要严查严办。于是，一个司级干部，被关在秦城监狱，“享受副部级待遇”。可以说，正是这种先入为主的指示，决定了一审的死刑判决。</div>

							
							
							
							
