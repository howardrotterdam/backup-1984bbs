													<h2>脑残不死 圣战不休</h2>
						
						
																											<div id="postmessage_666518" class="t_msgfont">今天是高考的第二天，在这里首先祝各位考生考一个好成绩。高考是每年的热点，不过在这里我倒不想对高考或者高考内容做评论，今天想关注更热门的一个东西，就是最近的有关“脑残不死，圣战不休”的活动。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;由于一些同学并不了解这件事，在这里做一个大致的说明，事件起因是由于世博会上，韩国馆组织了一次演唱会，有很多韩国明星会到场，然后很多歌迷自然就前去观看，由于之前在网上传言将会有5000张入场券，因此到场人数众多，后来，到场之后，实际票数少于网上传言那么多（有说法是一部分票被内部消化了），然后现场秩序非常混乱，歌迷与武警还发生了冲突，这一事件就是这次“圣战”的背景。事件之后，官方很快出面否认这一说法，但是网上的韩国歌迷，（有人认为特别是SJ的歌迷）对这件事颇有微词，同时很多人看不惯哈韩族平时对韩国明星的崇拜，于是矛盾一下激化，加上网上一些所谓内幕，或者当事人的帖子出来，于是各方开始有所行动，魔兽世界吧首先围观了SJ吧，然后SJ吧去围观了魔兽吧，到后面，事态扩大，天涯等加入了混战，同时这次各个贴吧的互爆也迅速上升成了“文化之间的冲突，甚至有关民族生死存亡的事件”，于是才出现所谓“脑残不死，圣战不休”的活动。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;从网上看到的资料，这次圣战党应该说站在道德的制高点，一些韩国歌迷的言论的确比较脑残，在这里附几个链接，可以看一下<br />
<br />
<a href="http://www.tianya.cn/publicforum/content/funinfo/1/2046171.shtml" target="_blank">http://www.tianya.cn/publicforum/content/funinfo/1/2046171.shtml</a><br />
<br />
<a href="http://www.tianya.cn/publicforum/content/funinfo/1/2048616.shtml" target="_blank">http://www.tianya.cn/publicforum/content/funinfo/1/2048616.shtml</a><br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;同时，从行动上来说，这次一些歌迷的行动也比较过火，或者说过于狂热，这方面的报道可以参见圣战组织的官网<a href="http://www.ak558.org/index00.html" target="_blank">http://www.ak558.org/index00.html</a><br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;这些是不是就意味着圣战是正义的，或者是我们应该歌颂甚至应该参与的行为么？理由的正义并不意味着程序的正义，更不意味着结果的正义，这几天来我们看到的都是清一色的肆意谩骂，看到的都是人身攻击，甚至我想如果双方可能见面的话，看到的也许就是流血了，从这里看程序不大可能正义，结果呢？估计也差不多。所谓的圣战本质上就和几十年前的文革一样，由少数人发动，意见不合就大打出手，互相争取所谓民意，群众斗争，暴民社会。也许有的人不同意这个看法，肯定会大声质问我那些哈韩NC不爱国，不爱民族文化，这难道能容忍吗?这些都是你们的借口，文革的借口也是这么光明正大，结果就是十年动乱，这里对文革同情的人就不用看了，一个人不是人的社会你乐意，我不乐意。没有正确的形式，再正义的借口也没有说服力。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;上面的论述局限于假设发动圣战理由是正义的前提下，事实上，这次所谓圣战针对的对象和行动已远远不是这个理由可以解释得了，它已经演化成了对喜欢韩国的人群的人身攻击乃至韩国形象的攻击，并且行动中也出现了去干扰韩国艺人的行为，这些非常值得商榷。作为一个人，一般而言你也许有偶像，当你想看你偶像看不到时，你会不会急躁，更何况人多时，什么人都有，踩踏并不是什么简直不可思议的事件，这里不为踩踏事件辩护，现有资料中的确有不少需要反省的行为，造成武警受伤甚至传言中的人员死亡，也的确是有过错，有过错不承认也做得不妥，但是这些就是圣战党攻击所有喜欢韩国的人和韩国的依据了？<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;说句实话，身边喜欢韩国歌的人肯定有，爱国的人肯定也不少，这些人平素也许毫无矛盾，因为这一件八竿子打不着的事，俩个人也许就开始互相谩骂，这件事也许最大的结果就是两类人的割裂，乃至民粹情绪的继续高涨，就像当年的烧法国国旗，抵制家乐福之类事。结果呢？我似乎没有看到任何变化，人与人的割裂似乎也不是爱国青年想看到的吧？<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;身边大多数人似乎对这件事最多的态度就是去围观，如果说难听点，这就是鲁迅先生所谓麻木的中国人吧，这件事用它浩荡的规模化作大家饭后的谈资，然后该干嘛干嘛，一切还是那样，我个人不觉得这有什么过错，因为我们都是普通人，关心自己关心的事是正确的，无论是哪边的主张其实离我们都很飘渺，当然，部分哈韩的主张只能说搞笑，这里只想说一句，人骨子里都是被压抑着的，一旦进入一个狂热的氛围，这种压抑无形就会松开，也许你就是下一个狂热的人。因此，请记住你做人的良知，你如果参与爆吧，你要想好你攻击的不是一个人，而是一群人，也许很多是无辜的，而且请不要用正义必须要付出代价之类的借口掩饰，说不定哪一天，你就会是那付出的代价。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;特别要阴谋论说一点，韩国朝鲜目前局势很微妙，如果有一天中国要在对朝鲜制裁上投票，那么请不要因为你憎恶韩国就支持朝鲜，敌人的敌人未必是朋友，更何况它还是一个恶魔。<br />
<br />
