																											<div id="postmessage_715006" class="t_msgfont">前言：问题的提出<br />
<br />
1. 文革与《参考消息》<br />
<br />
在一个自述里面，我说：“从小看《参考消息》，把言论自由和选举之类的事情当作公理，与中国国情格格不入。”<br />
<br />
从文革时代开始，我就感到奇怪：为什么中国人处理那些在国外属于一般性的事务，一定要闹得人仰马翻呢？阅读《参考消息》，我知道，像有人在街上喊个口号之类的事情是正常的。假如有人在大街上砸玻璃，关他几天到几个月，加上罚款就可以了。不怕关，不怕罚，一直砸下去的人并不多见。小事就是小事，不可以无限推演，进而提升到国家安危的高度。假如遇到大事，比如抢劫杀人之类的事情，按照刑法去惩办就行了。<br />
<br />
在国外，也有天下大乱的时候。此时，就需要宣布紧急状态了。开始的时候，我以为是要把捣乱者直接抓起来枪毙。随后发现，所谓的紧急状态不过是对现场的处置严厉一些，对某些新闻加以管制，以及逮捕、搜查和关押程序简单一些，特别是可以延长拘留期而已。<br />
<br />
当时我想，在中国，为什么日常的行政治理比国外的紧急状态还要严厉，这正常吗？<br />
<br />
2.1991年，《国家的制度化》<br />
<br />
改革以后，诸如民主法治一类的议论出现了。以往的想法被证实是对的，曾经让我激动了好长的时间。此后，终于发现了民主法治与过去的革命一样，只是随便说说而已。<br />
<br />
1984年，社会治安稍有恶化，一场“严打”就开始了。那一次“严打”，留下了种种荒谬和离奇的传说。在改革当中，同样充斥着长官意志、特事特办和大干快上。<br />
<br />
严×其先生的著述，给了我很大的启发。他站在宪法学的高度，一直强调治国要遵循法律和程序。他的《首脑论》，至今仍然是我们无法超越的。<br />
<br />
1991年，我写完了《国家的制度化》。《国家的制度化》指出，中国是一个非制度化国家。至于非制度化的原因，我归结为三点：清末以来乱世的影响，革命政治的影响以及小农意识的影响。我认为，中国必须走出乱世，在民主化的同时实现制度化。《国家的制度化》的重点，就是不可以使用非常时期的手段治理国家。“当国家恢复安定以后，就应该用常规的方法治理。”<br />
<br />
写完《国家的制度化》之后，一直想阐述国家的紧急状态和正常状态问题。由于要求太高，一直没有完成。此后，在论述其他问题的时候，部分地谈到了紧急状态和正常状态问题。<br />
<br />
3.以后的网文<br />
<br />
2007年4月20日，我写了《研究中国历史的两个线索》。线索之一，就是“专制的一般类型和极端类型；社会常态和紧急状态”。我认为，“秦战胜六国的原因其实并不复杂：在战国时代，各国实际上处于战争状态。商鞅变法除了有社会制度的因素外，主要是把国家置于紧急状态或者非常状态之下。商鞅的实用主义有部分的道理：那时，仁爱和道义确实没有直接的用途，让臣民无条件地忠于君主，甘心为国家送死才是取胜之道。”而在此之前的《国家的制度化》中，我这样说：“秦灭亡的教训之一就是无视前人和他人的经验，从一时的需要和短期的效果出发，一味实行严刑峻法。”<br />
<br />
2008年12月25日，我写了《不折腾：任务不能高于宪法和法律》。<br />
<br />
2008年2月24日，我写了《雪灾、紧急状态、效率与公信力》。文中提到了紧急状态及其限度，正常状态与紧急状态状态的区别以及必须实现社会管理的常态化。<br />
<br />
2010年2月4日，我在《修改《拆迁条例》，应该是为了民权与民生》中提出，要改变热衷以“紧急状态”模式治国的机会主义理念。<br />
<br />
4.一些说明<br />
<br />
需要说明的是，以自述的形式写出前言，从个人的认识过程说起，是为了告诉大家我不是研究宪法学和行政学的专业人士，因此读者对本文需要多做一些保留。<br />
<br />
我知道，研究紧急状态问题需要宪法学、法学和行政学的知识，这些是我不具备的。因此，我没有资格撰写这方面的论文。同时，我也有义务为学者今后的研究提供线索。我还想问：这么多年，研究宪法学、法学和行政学的学者们，你们干什么去了？<br />
<br />
我要感谢我的姑父潘玉深先生。文革时，他是教育局的干部，就是现在的公务员。他把《参考消息》带回家，让我看到了外面的世界。不仅如此，我还从他那里学到了很多政治方面的知识，他是我的老师。</div>

							
							
							
							
