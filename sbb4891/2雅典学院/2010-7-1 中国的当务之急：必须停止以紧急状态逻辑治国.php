																											<div id="postmessage_715004" class="t_msgfont">中国的当务之急：必须停止以紧急状态逻辑治国<br />
刘松萝<br />
<br />
摘要<br />
<br />
人类有文字的历史已经有几千年了。对于如何治理国家，已经积累了丰富的经验，这些经验是我们必须继承的。基本上说，国家的治理应该保持中道，遵守法律，并且尽量不打破常规。<br />
<br />
人类的历史是复杂的。走正道的人，不一定能够获得成功。就中国东周的历史来说，秦国得以战胜其他六国，除了商鞅之法中先进之处以外，还因为那时各国处于战争状态，秦国的军国体制正好适应了这种状态。<br />
<br />
在人类进步的过程中，出现过很多变革。为了实现变革，有时要打破旧有的制度。过于激烈的变革，被我们称为革命。有些国家在革命完成以后没有回复到常态，于是国家长期处于混乱的状态。遗憾的是，我们中国就是这样。<br />
<br />
由于清末以来乱世的影响，革命政治的影响，小农意识的影响以及领导人个人因素的作用，中国一直处于非常非常状态之中，处于非制度化状态之中。其具体表现，就是总是用战役和战术的思维，在人治之中，用突击的方法来完成日常的工作。<br />
<br />
有这样一种很奇怪，又被国人习以为常的现象：在中国，在太平的年月里，官员的权力竟然比很多国家在紧急状态之下还要大，采用的手段竟然比紧急状态之下还要随意。实际上，这是在以紧急状态逻辑治国。<br />
<br />
改革以后，情况一度有所好转。不幸的是，遇到危机和挑战以后，旧的思维再一次抬头，并且变本加厉。为了抵御经济危机，为了保持经济的高速发展，非常手段又成为首选。强迫性的，甚至是暴力的征地和拆迁，吏治的混乱以及腐败的加剧，都让我们看到乱世的图景。<br />
<br />
现在，已经到了摈弃以紧急状态逻辑治国的时候了。我们必须学会尊重法律和制度，用常规手段治理国家。我们必须认识到，任务不能超越宪法和法律，行政首长也不能超越宪法和法律。<br />
<br />
最后要指出的是，在实现以常态治国的进程中，知识分子负有不可替代，不可推卸的责任。知识分子有责任告诉公众和统治者什么是正常状态，什么是非常状态、战争状态和紧急状态；有责任告诉公众和统治者，在正常状态下必须用常规的方式治理国家。当国家出现异常的时候，知识分子有责任发出警告。<br />
