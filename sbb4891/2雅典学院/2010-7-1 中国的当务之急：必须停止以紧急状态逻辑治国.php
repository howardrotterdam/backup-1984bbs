																											<div id="postmessage_715010" class="t_msgfont">二、世界和历史视角下的治国之道<br />
<br />
关于治国之道，国外的经验更加完备。<br />
<br />
在接近4000年前，巴比伦国王汉谟拉比制定了一部法典，即著名的《汉谟拉比法典》。收藏在巴黎卢浮宫的《汉谟拉比法典》石碑上方，是太阳神和正义神沙马什授与汉谟拉比王象征权力的魔标和魔环的浮雕。也有人认为，这是沙马什在把法典授予汉谟拉比。浮雕不仅宣扬了君权神授的思想，也强调了法律的神圣性。这种法律神圣的意识，有助于法律的施行。<br />
<br />
在当时的社会条件下，《汉谟拉比法典》不可能是平等的。同时，《法典》的序言申明：“安努与恩利尔为人类福祉计，命令我，荣耀而畏神的君主，汉谟拉比，发扬正义于世，灭除不法邪恶之人，使强者不凌弱，使我有如沙马什，照临黔首，光耀大地”。在《法典》中，确实也有保护弱者的内容。值得注意的是，《法典》规定军人出征时，国家提供人力代为耕种，被俘后设法将其赎回等等。这些规定，表现了统治者对为其效力的军人的仁慈以及务实的治国风格。<br />
<br />
在古代希腊的一些城邦以及后起的古代罗马共和国，曾经实行共和制度，执政官由选举产生。公元前450年，罗马的《十二铜表法》完成了。以后，罗马共和国转变为罗马帝国。所谓帝国，就是以共和为名义的专制政体。在帝国时期，产生了高水平的私法。西罗马灭亡以后，在位于公元527到565年的东罗马皇帝查士丁尼颁布了《查士丁尼民法大全》。《查士丁尼民法大全》，是《查士丁尼法典》、《查士丁尼学说汇编》、《查士丁尼法学总论》和《查士丁尼新律》的合称。<br />
<br />
在西欧，取代西罗马的日耳曼人文化水平低下。同时，日耳曼人也有一些优点，比如王权有限，遵守秩序等等。特别是处于不列颠岛的日耳曼分支，盎格鲁-撒克逊人尊重惯例，讲究妥协。这些优点，后来随着英国和美国的强盛，对整个世界产生了不小的影响。<br />
<br />
1215年，英国《大宪章》诞生了。1640年，英国革命开始了。1775年，美国独立战争爆发。美国革命，产生了《独立宣言》和美国宪法。1789年，法国发生了大革命。法国革命产生了《人权与公民权宣言》。<br />
<br />
1945年，联合国成立了。《联合国宪章》、《世界人权宣言》、联合国《公民权利和政治权利国际公约》和《经济、社会、文化权利国际公约》的产生，标志着全人类终于找到了较为适当的既能够有效治理国家，又能够保障人权的途径。<br />
