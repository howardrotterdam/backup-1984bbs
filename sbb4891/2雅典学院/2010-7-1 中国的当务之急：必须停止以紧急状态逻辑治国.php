																											<div id="postmessage_715012" class="t_msgfont">三、对基本宪法原则的概述<br />
<br />
概括起来，基本的治国之道，或者说基本的宪法原则包含以下几个方面：法的神圣性；统治的有效性；对统治者的约束；国家的正义、仁慈和爱民；国家治理以及国民日常生活所需的明确和详尽的法律条文。<br />
<br />
现在，我们尽可以为新时代的民主、人权和物质享受而自豪。不过我们不要忘记，我们业已取得的进步不能自动地代替上述基本的宪法原则。<br />
<br />
我们有民主，有人权，有前人不敢想象的物质享受。但是，在很多国家，法律不再神圣。在菲律宾，民主没有带来秩序和发展。在法国和意大利，人性的张扬产生了像萨科奇和贝卢斯科尼那样的没有荣誉感的小丑级领袖人物。由于民主宪政也没有能够有效地节制资本的贪欲和疯狂，很多国家的贫富对立正在加剧。与此同时，我们消耗了太多的资源，以至于就要受到自然的惩罚了。在很多方面，我们并不如古代的优秀统治者。<br />
<br />
古巴比伦灭亡了，古埃及灭亡了，古希腊灭亡了，古罗马也灭亡了。这些古文明的灭亡，有自然条件的因素，有内部的问题，比如奴隶制度，也有外族入侵等等。<br />
<br />
从1512年英国《大宪章》诞生到现在，不过500年的时间。我们绝不可以认为，一切都尽善尽美了。各种古代文明的灭亡，仍然是我们的前车之鉴。<br />
<br />
因此，我认为，人类历史上各民族的治国之道，包括成功的经验与失败的教训，仍然是现代人类立法和治国的思想源泉。<br />
<br />
四、一些非经典的例子<br />
<br />
在介绍了历史上经典和著名的例子之后，有必要指出，大多数民族在治理方面都是有原则、有规矩的。<br />
<br />
现在，一些国家还存有历史上遗留下来的议事制度。比如，阿富汗在民族面临重大问题的时候，会召集大支尔格会议。在斐济，除了议会之外，还有大酋长会议。当然，大酋长会议有时扮演不怎么积极的角色，甚至被叛乱和政变所利用。<br />
<br />
历史上，蒙古的可汗需要经过库里台大会选举产生，直到忽必烈时代结束。满洲民族也长期设立议政王会议，直到康熙时代才宣告结束。<br />
<br />
1640年9月，在准噶尔部首领巴图尔珲台吉的倡导和支持下，联合喀尔喀蒙古札萨克图汗，在塔尔巴哈台召开了维拉特和喀尔喀的蒙古封建主会议，有各部44位蒙古封建主参加，制订了《蒙古卫拉特法典》。属于卫拉特四部的土尔扈特部，在从俄罗斯伏尔加河流域回归故土的艰难时刻，仍然遵守《蒙古卫拉特法典》，仍然坚持扎尔古会议制度。以后，留在俄罗斯，被称为卡尔梅克人的的卫拉特人，包括土尔扈特部、和硕特部和杜尔伯特，在很长时间里还在坚持《蒙古卫拉特法典》的权威性。<br />
<br />
此外，即使是处于原始状态的民族或者部落也是有规矩，有议事程序的。</div>

							
							
							
							
