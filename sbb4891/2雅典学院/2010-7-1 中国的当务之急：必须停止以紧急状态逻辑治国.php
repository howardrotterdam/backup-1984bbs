																											<div id="postmessage_715008" class="t_msgfont">一、中国传统思想：必须用正道，用常道治理国家<br />
<br />
在中国古代经典中，我们可以找到很多有关治国的论述。<br />
<br />
《论语》记载了传说中的尧把天下交给舜的时候所做的告诫：“咨！尔舜！天之历数在尔躬，允执其中！四海困穷，天禄永终。”（啧啧！你这位舜！上天的大命已经落在你的身上了。诚实地保持那中道吧！假如天下百姓都隐于困苦和贫穷，上天赐给你的禄位也就会永远终止了。）尧的这番话，包含两个重要的思想。一是治国要保持中道，二是国家和统治者不要让人民处于疾苦之中。<br />
<br />
春秋时代，主张超脱和相对主义的老子，在论述治国之道时正色告诉我们：“以正治国，以奇用兵，以无事取天下。”（以清静之道治国，以诡奇的方法用兵，以不搅扰人民来治理天下。）老子还说：“圣人云：我无为而民自化，我好静而民自正，我无事而民自富，我无欲而民自朴。”（圣人说：我无为,民心自然归化。我好静,民心自然匡正。我无事,我民自然富有。我无欲, 我民自然纯朴。）<br />
<br />
孔子认为，“政者，正也。子帅以正，孰敢不正？”（政就是正的意思。您本人带头走正路，那么还有谁敢不走正道呢？）孔子还说， “其身正，不令而行；其身不正，虽令不从。”（自己行为端正，不下命令，人们也奉行；自己行为不端正，即使下命令，人们也不奉行。）<br />
<br />
孔子主张国家要有等级和服从。据《论语》记载，“齐景公问政于孔子。孔子对曰：‘君君，臣臣，父父，子子。’公曰：‘善哉！信如君不君，臣不臣，父不父，子不子，虽有粟，吾得而食诸？’”（齐景公问孔子如何治理国家。孔子说：“做君主的要像君的样子，做臣子的要像臣的样子，做父亲的要像父亲的样子，做儿子的要像儿子的样子。”齐景公说：“讲得好呀！如果君不像君，臣不像臣，父不像父，子不像子，虽然有粮食，我能吃得上吗？”）<br />
<br />
同时，孔子又主张只有获得人民的信任，国家才能够存在。据《论语》记载，“子贡问政。子曰：‘足食，足兵，民信之矣。’子贡曰：‘必不得已而去，于斯三者何先？’曰：‘去兵。’子贡曰：‘必不得已而去，于期二者何先？’曰：‘去食。自古皆有死，民无信不立。’”（子贡问怎样治理国家。孔子说，“粮食充足，军备充足，老百姓信任统治者。”子贡说：“如果不得不去掉一项，那么在三项中先去掉哪一项呢？”孔子说：“去掉军备。”子贡说：“如果不得不再去掉一项，那么这两项中去掉哪一项呢？”孔子说：“去掉粮食。自古以来人总是要死的，如果老百姓对统治者不信任，那么国家就不能存在了。”）<br />
<br />
到了战国时代，商鞅在秦国变法。商鞅不是设立成文法的第一人，但商鞅之法更加详尽，执行起来也更加坚定。同样在秦国，以后韩非进一步论述了法家的思想，李斯则把法家法家思想付诸实践。法家的不足之处有二：第一，没有爱民的思想，一味严刑峻法，轻罪重罚。第二，不重视对君主的约束。这第二点，尤其是法家致命的短处。被称为法家集大成者的韩非，更进一步主张将法、术、势，也就是严刑峻法、阴谋诡计和强权政治结合起来治理国家。韩非否定基本的社会伦理，主张一切以帝王的利益为转移。因此，郭沫若先生曾经把韩非当做法西斯。<br />
<br />
综上所述，中国的传统思想主张国家要有等级和规范，也就是“君君，臣臣，父父，子子”；治理国家要保守中道，也就是“允执其中”；同时，统治者应该爱民。归纳起来，就是要用正道和常道来治理国家。<br />
<br />
古代先贤的语言很有概括性，并且具有强大的精神力量。像“四海困穷，天禄永终”，像“以正治国，以奇用兵，以无事取天下”，像“政者，正也”，以及“自古皆有死，民无信不立”，都会让我们过目不忘。<br />
<br />
在古代，华夏民族对君主行为的规范是通过礼仪、惯例和道德力量来实现的。孔子阐述的治国思想，有着基本法思想的意义。<br />
<br />
我认为，在肯定普世价值，肯定世界通行的宪法学原则的前提下，华夏先贤的治国思想也应该成为现代中国立法和治国的思想源泉。<br />
<br />
当然，与很多民族相比，华夏民族在宪政层面上的思想是薄弱的。</div>

							
							
							
							
