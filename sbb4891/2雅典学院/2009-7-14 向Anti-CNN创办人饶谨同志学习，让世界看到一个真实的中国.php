																											<div id="postmessage_161542" class="t_msgfont">看完央视的《对话》，饶谨同志的事迹深深地感动了我，他勇于挑战权威，为求真理不计个人得失，甚至甘冒生命危险的精神令我十分钦佩并自惭形秽，在如今这个“唯物”的年代，居然还有这样一个执着求真的青年！实在教我惊讶。节目中，在引用到“小事不惜财，大事不惜死”这句话时，他说自己还没达到这种境界，对此我决不相信！而且这短短几句朴实的话，又再次震撼了我，我感觉到，这是一种久违了的谦虚谨慎的可贵品质。节目接近尾声时，主持人陈伟鸿先生作最后总结，那句话我至今仍记忆犹新：“要让世界看到一个真实的中国”。要让世界看到一个真实的中国！这是多么激动人心的一句话啊！我相信，散布在世界每一个角落的华夏儿女都会为对这句话而感动，并产生强烈共鸣！这正是我们每一个炎黄子孙在新时代的神圣职责！借用饶谨同志的话来说，就是“只要是个正常中国人，有点良心，都应该去做这个事。特殊历史时刻需要有人出来担当一下”。饶谨同志，我想对您说：您决不是一个人在战斗！<br />
<br />
为了向饶谨同志学习，我提议，我们来建设一个能让世界看到一个真实中国的网站，让那些故意歪曲事实的报道再无立足之地！以下是我对建设这个网站的初步构想：<br />
<br />
中国幅员辽阔，每天发生的事多不胜数，怎样去解决这些信息的收集问题呢？很简单，只要我们每个生活在这片土地的人都愿意做一名记者，把自己身边发生的事忠实地报道出来（这些事可以是中国各地现代化建设的骄人成绩，人民生活改善的幸福场景，也可以是一些被传统媒体忽略的地带的事件直击，这固然包括一些所谓的“负面消息”），稿源的问题即可迎刃而解，而且由于信息来源遍及全国各地，当它们汇集在一起时，就能构成一幅比较完整的中国当代社会图景，同时也降低了“片面报道”存在的风险。在此，我想特别说一下我对所谓“负面消息”的看法。有人可能会认为，报道“负面消息”有损国家形象，我不赞同这种说法。因为要是故意回避“负面消息”，那就是选择性报道，也就是不实报道（打个比方：例如报道一起斗殴事件，如果只说甲如何如何打伤乙，而对乙挑衅及伤害甲的行为却不作任何报道，那肯定会令读者产生错觉――认为甲要负全部责任，而乙则成了一名无辜受害者）。而且，这种报喜不报忧的做法，即便可以打造出无比辉煌璀璨的景象，它也始终是一个虚假幻象，它呈现给世人的并不是一个真实的中国，而是一个缺乏自信、虚伪、脆弱的中国。因此，我们必须对正面和负面的消息采取一视同仁的态度，坚持实事求是、客观公正的原则，不进行选择性报道。唯有这样，才能让全世界对中国有一个更立体的认识，才能让世界看到一个真实、自信、朝气蓬勃的中国。<br />
<br />
接下来讨论网站的命名，我建议网站中文名叫：真实中国，英文名：true-cn，国际域名：true-cn.com（希望还没被抢注），大家觉得可以吗？希望大家多提宝贵意见。我想，只要大家齐心协力，这个构想就一定能实现。别再犹豫不决了，是时候了，让我们一起来向饶谨同志学习，学习他无私无畏、实事求是的伟大精神，让世界看到一个真实的中国！<br />
