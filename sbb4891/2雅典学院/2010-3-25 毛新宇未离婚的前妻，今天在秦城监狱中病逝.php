<br />
&nbsp;&nbsp;在御座宾馆短短两天的接触中，郝明莉引起了邵华的注意，并喜欢上了这个漂亮的山东姑娘。毛新宇一行人在泰安住了两天后，要去曲阜参观旅游，邵华想让郝明莉一起去曲阜。由于事先未做说明，也由于郝明莉的身份，省市接待办同志没有同意。几天后，郝明莉接到省接待办一个同志的电话，转达了毛新宇一家人对她的谢意。<br />
<br />
&nbsp;&nbsp;毛新宇、邵华、张文秋一行在曲阜、济宁参观完后，由于旅途劳累，张文秋老人患感冒，住进了泰安市人民医院。所以，毛新宇一行又住到了御座宾馆，郝明莉第二次见到了毛新宇。<br />
<br />
&nbsp;&nbsp;后来，郝明莉又接到几次电话，讲毛新宇对她印象较好，希望能多联系。休班时，郝明莉回到家中，对她父亲讲了这件事。郝德望听了女儿的话，开始并没有往心里去，自己家是中国众多的普通家庭中的一个，毛新宇的家庭却是人人皆知的伟人之家，全中国只有一个，郝德望对女儿讲，这个事情一定要慎重，关系重大，千万要处理好。郝明莉只是点头，让父亲放心，她会处理好此事。<br />
<br />
&nbsp;&nbsp;1994年5月，郝明莉又接到电话，邀请她去北京玩，盛情之下，郝明莉来到毛新宇家中，住了短短的一个星期。<br />
<br />
&nbsp;&nbsp;在这短短的几天中，郝明莉始终被一种浓浓的亲情包围着，邵华平易近人，对她女儿般的体贴照顾。尤其是毛新宇，更是在她心中留下了深刻印象。毛新宇聪明好学，知识面极广，待人也宽厚。这次来北京，并没有涉及到婚嫁问题，邵华称郝明莉为自己的干女儿。考虑到郝明莉既上学又工作的境况，邵华一家提出不如将学校转到北京来读，更能充实自己。1994年6月底，郝明莉在泰安御座宾馆办理了辞职手续。同时，也去山东矿院办理了转学手续。来到北京，郝明莉经过考试，进入北京大学国际商务系就读，成了北京大学的一名学生。1997年夏天，郝明莉从北大毕业了，同毛新宇的爱情也到了瓜熟蒂落的时候。邵华、毛新宇向郝明莉提出了结婚事情。后来又通过别人打电话给郝德望，提出关于毛新宇与郝明莉的婚事问题。在郝德望看来，此事还是慎重一点好。随后，双方又通了几次电话。毛新宇、郝明莉认识好几年了，相互之间很熟悉了，邵华与郝德望才将儿女婚事确定下来。<br />
<br />
<br />
第二部分 郝明莉现在到底怎样？<br />
<br />
来源于一个作者的网络资料，没有任何确实资料可查<br />
<br />
毛新宇的第一任妻子竟然2003年死于秦城监狱！？<br />
<br />
关于毛新宇于2003年喜得贵子的新闻，许多媒体都报道了，网上也多有转载。可是，今天才在一个毛新宇一家三口出席某座谈会的帖子看到，毛新宇现任妻子竟是“第二任”（帖中注明）。这个消息对很新鲜，因为此前多从没听说过毛新宇离婚结婚的事。对于毛新宇1997年结婚的事媒体也是作了大量报道，所以印象深刻，不想2003年为毛新宇生下儿子的并不是原配而是“另有其人”，也即第二任妻子刘滨。<br />
<br />
于是，在网上搜索了一下，果真如此，毛新宇经历了二次婚姻。第一次结婚在1997年，第二次是在2002年。但是关于毛的第一次婚姻的破裂相关信息非常少。<br />
<br />
当然结婚两次也没有什么，可是却在一个帖子看到这样的话：“毛新宇的前妻郝明莉，在2002年五月，由於得知毛新宇又有新欢，而和毛新宇闹翻，被秘密关进秦城监狱，于今天早晨在狱中病逝（帖子时间是2003年12月）。”这个消息让人不禁有些惊骇。第一次婚姻究竟为什么离婚（似乎是说第一位妻子两年没有生育，但是这个说法不知是否成立）？离婚后为什么要把人关进秦城监狱？关进去为什么那么快突然死亡？</div>

							
							
							
							
