													<h2>我来告诉你公务员的工资到底是多少</h2>
						
						
																											<div id="postmessage_702161" class="t_msgfont">声明<br />
<br />
你可以不同意别人的观点，但请给别人表达的自由;<br />
<br />
欢迎各种意见，理性交流，但谢绝人身攻击、谩骂和人肉;<br />
<br />
其实以前在其他论坛上我也发表过和以下类似的观点，其实天涯上一直都有公务员收入是偏高还是偏低的争论，今天看到了大拿杂谈和堂吉诃缺德两人的论战，然后很多人也开贴，各说各的理。我认为此时有必要也出来澄清一些事实，但不想陷入纷争中，我的观点不代表除我之外的任何人的观点。<br />
<br />
请大家在阅读完每一句话后再进行评论，我说的不一定全对，但是一定凭良心、尽量客观的表述。<br />
<br />
进入正题前，给大家讲述一个昨天发生在我身上的事情：<br />
<br />
前天(6月21日)晚上加班到快11点了才打的回家。的士司机态度很好，一路上我们交谈甚欢。我询问了下的士司机的收入和工作的辛苦度，他也向我询问了工作和收入等等。当我告诉他，我在中央某部委工作，工作的内容主要是针对外国企业、维护国内企业利益时，他很是惊讶、羡慕和赞赏。然后，突然问了我一句：那你们的收入一定很高吧，一个月没有两万也有一万吧?<br />
<br />
这位司机的发问让我觉得哭笑不得。因为快到住处了，我打开车门前简单和他解释了一下：我每个月工资3200元左右，加上每个月房补800元，扣除单位单身宿舍租金400左右(两人一屋，一人200，我一个人住所以掏双份)，一个月下来就3600元左右。当我关上车门的那一刻，挂在司机脸上的表情，好像是被我的话深深雷到了：除了惊愕还是惊愕。<br />
<br />
为了客观公正，我公布下个人的工作经历和收入情况：<br />
<br />
2007年7月我毕业于北京某高校。2007年7月至2009年7月在北京市某基层法院工作，2007年7月至2008年7月为试用期，2008年8月转正任副主任科员。2009年7月进入某国家部委，现仍在试用期内。<br />
<br />
2007年7月至2008年7月，我每个月工资1700-1900元之间，加上季度加值班费1200X4=4800元，加上4个过节费各1000元共4000元，加上年终奖5000元。试用期那一年我总共的收入是35000元左右，月均不到3000元。2008年8月至2009年7月，也就是转正后第一年，我每月工资2700元上下，加上季度加值班费2200X4=8800元，加上4个过节费各1000元共4000元，加上年终奖10000元。<br />
<br />
在法院的第二年我的总收入是56000元左右，平均到每个月4700元左右，转正之后级别不变，工资就不变(对了，每年会加几块工龄钱)。2009年8月至今我每月工资是3600元左右，将于7、8月份转正，转正之后工资收入会多200-400元，即转正后工资还达不到4000元。前几年工资改革后，中央国家机关没有过节费、加值班费和年度奖金。所以说，我离开北京市基层法院去国家部委，我每年的收入要缩减15000元左右。我以我的良心保证上述资料的相对准确性(上述数据误差在10%以内，没法精确，但倾向于取中间值)。<br />
<br />
我是在北京市阳光工资改革后参加工作的，所以我在北京市的工作收入水平可以基本代表北京市同一级别的公务员收入水平。根据官方统计，2007年北京市职工平均工资是39726，2008年北京市职工平均工资是44715，那么我本人2007年7月至2008年7月的工资水平比上述数据低，当然试用期是主要原因。2009年北京市职工平均工资至今还没有发布，但估计在50000元上下，那么我本人2008年8月至2009年7月的收入水平略高于上述平均数。<br />
<br />
因此可以说，北京市公务员的工资收入水平与社会职工平均收入水平相当或略高。我现在在某国家部委工作，工资收入比在北京市时低，估计也就是北京市职工工资的平均数，而且中央部委的工资差距更小，因此我现在的收入水平也能代表其他国家部委同一级别的公务员收入水平。<br />
<br />
但是，公务员的薪金水平要是就和社会职工平均水平相当那是很可怕的，贪污腐败现象就不可避免了。<br />
<br />
首先，进入公务员系统的都是前期进行大量教育投资的群体;其次，公务员代表国家行使管理国家的职权，理应享受相对丰裕(注意是相对)的收入;第三，世界上没有一个国家公务员的收入水平比社会职工平均收入低或者仅仅略高的，如果有，那个国家一定无政府存在，因为大家都不当公务员了，相反世界上大部分国家的公务员实行高薪养廉的政策，在一定程度上抑制了贪污腐败。<br />
<br />
因此，我可以负责任的说：北京市和中央国家机关公务员工资水平确实偏低，其他地方的我不敢说。<br />
<br />
但是，为什么公众对公务员收入会产生误解呢?我想原因主要有(按照重要程度排列)：<br />
<br />
公众误解的根源一：贪污腐败现象严重<br />
<br />
公众误解的根源二：三公消费数额巨大：公款吃喝、公车私用、公费出国<br />
<br />
公众误解的根源三：国家财政支出不够透明<br />
<br />
……………<br />
<br />
对于上面的三大理由以及其他可能的理由不做具体的说明了，大家也能想到。<br />
这里我想说的是：为什么排除那些贪污腐败分子外的好公务员、小公务员的收入如此之低而仍然被公众误解呢?<br />
<br />
首先我想引入自创的两个心理学上名词：过错分摊效应和滴墨染缸效应(寡人孤陋寡闻也许心理学早已有描述这个现象的专门词汇，但本人仍对这两个名词享有著作权呵呵)。具体说：<br />
<br />
过错分摊效应，是指在一个群体里，只要其中有一个个体犯错，那么其他个体也会因这个个体的过错而承担本不应该承担的责任。用一道数学题来解释更简单一点：A是一个公务员，他贪污了10个亿，那么全国2000万公务员被分摊了50元，即全国每个公务员都贪污了50元。<br />
<br />
滴墨染缸效应，是指在一个群体里，只要其中有一个个体犯错，那么其他个体也会因这个个体的过错而受到外界的同一谴责。形象的说，就是我们把一滴墨水滴进一个鱼缸，那么这个鱼缸的水全部被污染的。其中的每一滴水跳到黄河也洗不清了。<br />
<br />
其次，我想从先入为主情节以及人所处位置的不同进一步解释为什么公众存在这种误解。<br />
<br />
先入为主也是一个心理学名词是。工资改革前，我不知道具体情况，但是大概情况是各部门各种福利、津贴满天飞，但是工资改革后，这些福利绝大部分被取消了，这种情况2005、2006年(改革前后)参加工作的人感觉比较深切。还是以我为例吧，刚进去法院的时候，听老同志说，工资改革前每个月平均工资不下8000元(全部算上)，年收入10万以上，我靠，我第一年试用期，卡上工资不到2000元，全部折算进去不到3000元(参看上述数据，进行乘除)。而被压迫的人们大众一想到公务员加薪，自然而然想到了之前的情况，但实际情况确实变化很多了。<br />
<br />
人所处位置的不同影响人的判断和观点。进入公务员系统之前，我也十分反感公务员加薪，因为在我的心中，公务员的地位、收入很高，而广大的老百姓生活在水深火热之中。但是当你真正进入这个系统后，我才发现情况正不全是原先想象的，准确的说法是：广大老百姓生活在水深火热之中，而守法公务员，特别是小公务员也生活在水深火热之中。一个人所处的位置在很大程度上影响了他的理想思维，进而影响了他的判断。如果，大家能平心静气，以数据为基础进行思考，那么误解和冲突可能会避免，至少会有所缓解。<br />
<br />
其实，只要理性的思考一下，公众的观点是存在一定误解和偏差的，但是作为一个整体但实际分散的公众容易受到他人的影响，不容易进行理性思考，所以误解和偏差就在所难免了。因此，我们在理解公众的误解和偏差的同时，公众也要努力得客观理性的去思考。<br />
我知道很多人会在回帖中问下面的一些问题，我试着先回答几个：<br />
<br />
1、 公务员还有其他灰色收入啊。<br />
<br />
工资改革后，公务员的工资都在账面上，个别的补贴也大多整合到工资里面，比如交通补贴、通讯补贴。其他的我不知道还有什么?如果有人硬要说，贪污腐败、受贿索贿啊。那楼主我要告诉你，我是想通过这个帖子告诉大家，除了贪污腐败之外的公务员的真实收入。贪污腐败是个体行为，自有相关部门来处理他们，但请不要把棒打无辜。前面关于两个自创的心理学术语已经说明了，这种棒打无辜现象缺乏理性。<br />
<br />
2、 国家有福利分房啊，这也是收入啊。<br />
<br />
福利分房在上世纪末基本上停止，当然后来演变成集资建房，再后来又被禁止了。现在的中央国家机关还存不存在福利分房呢?有，但已经很少很少了，除了几个有实权的部委，其他的部委想拿地越来越难了，加上中央对这块的控制越来越严，可操作的空间越来越小。还有就是，即使存在零星的福利分房，那也是价格很高，与市场价格的差距越来越小。反正我在的部委越来越没有希望了。<br />
<br />
3、 公务员即使没有福利分房，也有宿舍啊。<br />
<br />
是的，一般国家机关都会给职工安排宿舍，但都是单身宿舍(一般两人一屋)还要房租，大概是200元每月，因为我一个人住所以交两份共400元左右。或者中转房(个人没有产权，但有有限的使用权，在部委里要按资历排行，我们这些新人根本没戏)。原来在法院的时候，第一年试用期没有单身宿舍住，我月入1700元多，1300元房租在南三环，每个月女朋友现在的老婆救济。现在结婚了，孩子有了还是租着房子1400元，加上单位宿舍房租400元共1800元，我的工资扣除之外剩下2000元左右。这就是中华人民共和国中央国家机关一个副主任科员的收入啊。说来想必没有几个人相信，拿掉头上“中央部委”的光环，什么都不是。<br />
<br />
4、 公务员工资那么低，为什么还那么多人进入这个系统。<br />
<br />
是的，在任何一个社会，即使动荡的社会，公务员都是铁饭碗，饿不死。但是社会上总有人饿死啊。再者说，有人进入这个系统本身就是心存不轨，比如就想贪污腐败。刨除这些人，有人喜欢当清苦的公务员你又有什么意见，有人喜欢自己获得较高的社会评价你又有什么意见，人家爱怎么选择那是人家的权利。说到我自己，其实我是不想在公务员系统长呆，但是各种原因吧，暂时委身于它，有机会我会逃离的。<br />
<br />
………<br />
说了这么多，我无非想表达几个观点：<br />
<br />
1、 现价段(工资改革后)公务员的收入水平并不高，这是导致贪污腐败的一个原因，高薪养廉是世界上大部分国家实行的政策。<br />
<br />
2、 公众基于各种原因和认识偏误(过错分摊效应、滴墨染缸效应、先入为主情节及站在自身角度思考问题等)，对公务员收入存在误解和偏见，希望大家多站在不同的角度思考问题。<br />
<br />
3、 贪污腐败要严厉打击，三公消费要严格压缩，财政账目尽量公开透明，只有这样，才能减少公众的认识偏误，引导公众进行理性思考。<br />
<br />
4、 单纯的仇官仇富缺乏理性，我们要仇该仇的官，要仇该仇的富，不要一棍子打死全部。<br />
<br />
帖子中漏了公积金的事，这里补上。我的情况是：公积金我交393，单位交393。大家也可以用这个数反推一下我的收入。这个吧，比在法院的时候又少了近300元（总共）。亏啊。<br />
<br />
后记一：前面说了，每个人的位置、身份在一定程度上影响了他的思考，进而可能影响他的结论，我不敢保证我这篇帖子没有受到我是公务员这个身份的影响，但是我可以负责的说：我没有故意得去隐瞒什么，我说的都是尽量客观真实，尽管可能不够精确。<br />
<br />
后记二：这个帖子是我在天涯安家几年来第一次最认真，也是写的最长的帖子，筒子们如果不同意我的观点，可以拍砖，但是请留活口。谢谢国家、谢谢领导、谢谢父母。<br />
