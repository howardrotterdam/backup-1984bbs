																											<div id="postmessage_496329" class="t_msgfont">新年好心提醒：陌生人随时可能带来杀身之祸<br />
<br />
<br />
在网上看到一篇新闻，看后即想把近年心里边酝酿的一个想法说一说，大家最好都看看俺的此文，对大家今后出外的人身安全，将是功德无量。<br />
这篇新闻说的是四川大学一位退休教授张中一先生，今年春节前在云南旅游，因一极小之事惹来杀身之祸，命丧云南元阳县。<br />
张教授与老伴一块到元阳旅游，某日清晨到该县一著名的观看梯田日出景点搞摄影，此处乃是摄影爱好者喜欢来的地方，故而觅位摆设三脚架竞争激烈。张教授摆设完三脚架后，一位女游客声称位置是她早就占定的，张教授只好收起来准备离开，但二人产生了一些口头争议，张教授不过说了句：这里是公共景点，不能因为你来得早，就把位置占完吧？女人大恼，随即大喊大叫招来其丈夫，恶人先告状声称张教授打她，其丈夫对张教授拳打脚踢，很快将其逼至观景台边缘，仍无意收手，终将张教授打得失足跌下台去，后脑着地，形成重伤，送至医院抢救不治身亡。<br />
这篇报道就引发了俺的许多记忆，其实，在当今社会中，公民之间，一言不合，即大吵大闹，冲突愈演愈烈，最后导致恶性事故者，极多。大家可以想想自己的所见所闻，只不过上边讲的例子，比较恶劣、比较典型而已。<br />
俺就记得郑州媒体报道过不少起事故，俩公民素昧平生，在公共场合因些微摩擦，当即对吵、对骂、对打，尔后一方受重伤或死亡。去年某天俺还在经三路某路口看到，一辆大客车想拐弯，但前边有一骑电动车的小伙子在客车前等绿灯（此处没有自行车道），大客车大鸣喇叭，电动车不为所动，司机便大声呵斥，小伙子也仰头对吵，司机提了一把扳手就下来要砸小伙子，那种穷凶极恶的样子让路人侧目，小伙子吓得围着电动车乱跑……<br />
其实不用俺提醒，大家日常肯定好多类似记忆、类似观感。通过类似“南京老太”或者“上海钓鱼”的教训，大家都晓得了，随便帮助陌生人，是会吃官司、被罚款的；但是俺要进一步提醒大家：随便和陌生人发生冲突，就不是吃官司、被罚款的问题，而是可能被暴打、甚至丢掉性命。<br />
俺要更进一步提醒大家的是：有些争执、有些冲突可能很小，但随时都可能变成流血冲突；那些陌生人看似普通正常，根本不是流氓小偷黑社会，但极可能随时都变成暴徒。<br />
咱们来看一下：在发生在元阳的事件里边，那个恶女名叫吴姣，其丈夫名叫许刚，许原来系广东足球运动员，退役后到武汉从商，身家颇富，按说有身份有地位，即便现在富人都有盛气凌人的恶习，但总不至于想把人打死葬送美好生活，许把张教授打到台下，看人不行了，整个都傻了，在警局还忏悔说，都是这个老婆害的，平时她就多事。<br />
可是，与他们相遇，谁知道吴女多事不多事，谁知道这些衣冠楚楚的人转眼就可以因为一点争执就把人往死里打？谁知道不让路就会遭到那位客车司机如此粗暴的行动？<br />
这些一般人往往都不能预测。但这些事情普遍发生，就不能不让人感到：这中间有某种必然性，偶然的契机下边是一种必然的东西。某种暴戾的社会情绪的蔓延和侵染。暴戾，就是粗暴和戾气，就是遇事不论理，不理性，不理智，不和平解决，有理无理都要强占三分，不是你压倒我，就是我压倒你；不是你让我蒙羞，就是我让你丢人。往往根本就没有妥协退让、互相宽容一说。<br />
它是如何形成的？俺认为这是一种“丛林情绪”。丛林情绪的背后是丛林环境和丛林法则。你看看周遭是不是弱肉强食在横行？有权的可以随便欺负老百姓，随便把你当“屁民”，权力寻租动辄贪腐百万千万，小老百姓还奈何不了他；没权的，即便处在食物链最下线，也有他反咬你的时候，最近说海南豇豆发现剧毒农药含量，农民可以说比较苦的，但农民也有奴役你的时候，用毒豇豆，注水猪，超生，反正几乎人人都有不按规矩出牌的机会，你算计了我，我也会算计你，你屁民我，我毒死你，不毒也恶心死你。有钱的富人炫富一掷千金，没钱的屁民在网上骂死你，心里想的是逮住机会宰了你；有钱的开车横冲直撞，没钱的贱民去碰瓷讹诈……<br />
我们的社会有没有丛林化？俺不知道，俺只晓得社会民众越来越浮躁，越来越焦虑，他不可能去修理有权的，人家即便骂你是屁民、怒喝你为谁说话，不还是官照作，败照腐，周九耕这样的才有几个，恐怕比交通事故率都低（这是有人论证过的）；他也不可能去修理有钱的，你看重庆的有钱人多少H社会，别处怎样？就连北京的艺术家，遇到强拆你又有何办法？还不是人有棒槌，我有脑袋？棒槌就是H社会，有网友已经说了，经济危机让屁民更没安全感了，因为买一条大腿，现在的行情据说是数千元就可以了，而且还有价格竞争，你说到了一块钱一条人腿时候，谁是安全的？――当然这只是笑谈，可反映出现今权、钱、拳头是硬道理这么一个真谛。<br />
面对这样的情况没有人可以说是心平气和的。一旦遇到小小纠葛，就会爆发出来，没有钱没有权我还没有拳头？受够了有钱的有权的欺负我还受你丫一瘪三欺负？你以为你是谁？你就是比尔&#8226;盖茨的孙子我也打定了，有种你把我掐死、毒死、电死、躲猫猫死，不是你死就是我亡，东风吹战鼓擂这个世界谁怕谁？这叫怒从心头起，火向胆边生，于是乎，理智已经淹没，理性已经退场，惟一剩下的就是兽性。一旦清醒，追悔莫及，但身在事中，很少有人能控制心中的暴戾之气，那是整天整夜都在积蓄的，就像国人习惯于往银行里存钱一样。<br />
暴行发生后许多熟人都觉得不认识当事人啦。这种情况太多了，俺懒得一一列举，单说去年成都一言不合杀死女大学生村官而轰动网络的李斌先生，许多熟人事后都觉得不可思议，这样一个人平时太文明礼貌了，咋可能干这种事？定然是女村官出言不逊、极端污辱。而抨击李斌的人则认为他是个大恶棍。实则或许我们每个人在本质上都是这样的好人加恶棍，只不过在文明社会恶棍的成分得到了疏解而不是聚集，不像我们这样每日每夜像往银行里存钱一样储存着仇恨、暴戾、埋怨、妒忌和报复心。<br />
一般而言，在中国这样一个“熟人社会”、“人情社会”和“面子文化”之下，我们在熟人圈里都不会表现出这种丛林化的暴戾，因为需要面子，需要人情，需要关系的提携。而这样就更使得暴戾之气没有排泄口，一遇陌生人的纠葛，便汹涌而出，一泻千里。中国文化对陌生人也是非常戒备的，就是不认为陌生人是好东西，不必要都他客气，所以讲中国文化的仁义礼智信的时候，我们不要忘记，这些所谓的好东西它们的赠予范围是很小的，就是熟人、亲朋圈子，舍此就是非我族类，其心必异。所以俺同样觉得，中国古代咱们的祖先生活的环境也有丛林化的嫌疑，要不为啥总是这么多不安全感？<br />
罗嗦了这么多废话，要点是想提醒大家：出门在外切勿和陌生人发生冲突，最好不和陌生人发生任何关系，与之绝缘，方能规避根本不可预测的冲突和暴戾、暴力。因为现今每一个行走在路上的陌生人都是一座被愤怒和暴戾炙烤的火炉。<br />
当然，这话的意思是，下次变成许刚、李斌那样的暴徒恶棍的，也可能是正在看这篇文字的诸君您。<br />
多么希望每天每天我们在路上遇到的、看到的，皆是一张张健康清新、生动阳光、温暖礼仪的微笑的脸，皆是一声声类似于国外的“hello”的“您好”、“需要帮助吗”的问候，皆是一份平心静气和从容淡定、优裕自如。但是现在，我们还是要带着一张张忧心忡忡、冷漠麻木的老脸上路，还是要带着一颗颗宛若身处丛林般的忐忑不安、怒火中烧的心过活。上帝，请你保佑这些已经不知道爱与平等为何物的人们吧，或者把他们送火星也行！</div>

							
							
							
							
