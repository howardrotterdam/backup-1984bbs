2010年08月06日 11:51中新网<br />
中新网8月6日电 香港《明报》6日刊文《胡锦涛教民众如何生活》说，日前在中共中央政治局集体学习中，胡锦涛罕见地对深化文化体制改革发表讲话，为内地文化建设方向定下总调。分析称，胡锦涛对文化体制改革提出许多新论断、新观点，科学发展更深层次涵义开始指向“如何生活”，为民众创建新的生活方式开路。<br />
<br />
文章摘编如下：<br />
<br />
日前在中共中央政治局第22次集体学习中，胡锦涛罕见地对深化文化体制改革发表讲话，为下一阶段内地文化建设方向定下总调。<br />
<br />
分析人士称，胡锦涛对文化体制改革提出了许多新论断、新观点，科学发展更深层次涵义开始指向“如何生活”，新文化建设运动的开始即是为民众创建新的生活方式开辟道路。<br />
<br />
8月2日至5日，官方媒体对胡锦涛的讲话进行了密集解读。《人民日报》连续4天在头版发表评论员文章，称“经济之‘危’，往往是文化之‘机’”，中国文化建设“正迎来历史上最好的发展时期，推进文化体制改革向纵深发展的一系列有利条件已经具备”。<br />
<br />
大推文化建设改革运动<br />
<br />
在2007年十七大时，中共对中国特色社会主义发展总体布局提出“四位一体”理论。其中，政治建设、经济建设在近年来甚有声色，文化建设和社会建设则一直偏弱。<br />
<br />
新一轮文化建设运动藉中共理论家之口提出了“讲发展必须讲改革”的口号，并且在多处透露出未来文化体制改革将“不破不立”的信号。《人民日报》的几篇评论提出，文化的全面繁荣“正是建立在新体制对阻碍文化发展旧体制的突破之上”，要“冲破观念的禁锢，打破体制的障碍，完善政策的支撑”。<br />
<br />
胡锦涛明确提出深化文化体制改革有4项重点，可概括为“三个加快、一个加强”，即加快文化体制机制改革创新、加快构建公共文化服务体系、加快发展文化产业、加强对文化产品创作生产的引导。<br />
<br />
分析人士称，本轮文化体制改革除有提升中国国际地位、增加在国际事务中的参与性和话语权等实用考虑外，对内的主要目标是“满足民众精神文化需求”。<br />
<br />
新华社8月3日发表评论员文章提出，未来文化体制改革“要突出政府提供公共文化服务的责任”，此外，还要“突出文化产品‘以文化人’的功能”。(钟鸣九)</div>

							
							
							
							
