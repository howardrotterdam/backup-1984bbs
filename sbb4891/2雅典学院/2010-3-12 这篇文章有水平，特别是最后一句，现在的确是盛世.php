																											<div id="postmessage_514346" class="t_msgfont">中国历史上真的有“禅让”吗？我说的真的“禅让”，是指君临天下的帝王从内心愿意把天下托付给非直系血缘的人。事实上，查有实据的，中国历史上只有一次真诚的“禅让”，但这次禅让却是一次名副其实的悲剧。<br />
<br />
&nbsp; &nbsp; 如果从黄帝开始考察，五千年前中国并没有实行所谓的“禅让”制。黄帝去世后，帝位传给了他的儿子少昊，少昊传帝位给侄儿颛顼，颛顼将帝位传给了侄儿帝喾，帝喾将帝位传给了长子帝挚，帝挚荒淫无度，诸侯废之，而推尊帝挚的弟弟尧为天子。可以这么说，从尧以前，黄帝家族一直是“家天下”的，虽说有时传侄，有时传子。中国流传的所谓“禅让制”，是从尧开始，传说尧禅让给了舜，舜禅让给了禹。世袭制的家天下到了中途突然变成了禅让制，从开始流传就遭到了人们的质疑。<br />
<br />
&nbsp; &nbsp; 首先，舜也是黄帝家族人，是黄帝的八世孙；而传说禹也是黄帝族人，为帝颛顼的曾孙，黄帝轩辕氏第六代玄孙，“便宜”没出当家。另外，尧将两个女儿娥皇、女英都嫁给了舜，说白了舜是尧的女婿，是“半个儿”，到底没出一家人。即便这样，尧也不是自愿传给舜的，古本《竹书纪年》记载：“昔尧德衰，为舜所囚也。”，尧禅让给舜，有“逼宫”的嫌疑；舜是主动禅让给禹的吗？按道理来说没有任何可能，舜曾杀鲧，是禹不共戴天的杀父仇人啊，将帝位传给仇人，太不符合人情道理了。所以这一禅让几千年来一直受到质疑，而“禹像当年的舜一样发动军事政变，篡夺了舜的帝位，把舜流放到广西苍梧，报了杀父大仇”之传说倒正符合人之常情。<br />
<br />
&nbsp; &nbsp; 秦始皇之后，中国历史上发生了不少“禅让”的闹剧，如西汉末年的王莽篡汉，是王莽用禅让的手段强夺了孺子婴的江山；三国时曹丕逼汉献帝“禅让”，建立魏朝；其后司马炎照方抓药，逼魏元帝曹奂“禅让”，建立晋朝；再有就是刘裕逼晋恭帝司马德文禅让，建立刘宋政权；萧道成逼宋顺帝刘准禅让，建立齐政权；萧衍逼齐和帝萧宝融禅让，建立梁政权；陈霸先逼梁敬帝萧方智禅让，建立陈政权……不管是哪一次禅让，帝王都是宝剑驾在脖子上“禅让”出了江山，而且没有一个有好的下场。<br />
<br />
&nbsp; &nbsp; 史上有真诚的禅让吗？很稀少。刘备倒是有“禅让”的思想准备，他临终时对诸葛亮说：“君才十倍曹丕，必能安国，终定大事．若嗣子可辅，辅之；如其不才，君可自取”，说的情真意切。不过，世人都认为刘备并没有真的想将江山禅让给诸葛亮，而是用此言试探诸葛，激诸葛亮好好辅佐阿斗。史上唯一真诚的禅让，发生在战国时期的燕国。<br />
<br />
&nbsp; &nbsp; 燕王哙是燕国第三十八任国君，他在位时，非常仰幕古代尧、舜、禹的禅让之德，习学儒道，比那个愚腐的宋襄公还迂。迂腐的燕王哙还非常想通过“王道”称霸诸侯，他问苏代：“齐王这个人能称霸吗？”，苏代回答：“不能称霸”。燕王哙问为什么？苏代回答说是因为齐王“不信其臣”。于是燕王哙接受齐王“不信其臣”的教训，充分信任其丞相子之。殊不知苏代所言，正是受了子之的贿赂，子之是谁，与苏代的族兄苏秦是儿女亲家啊！子之是个阴谋家，他通过其他大臣一步步说服燕王哙进入他设计的“圈套”。一个他买通的叫鹿毛寿进一步对燕王哙说：“您不如把国家让给国相子之。人们之所以称道尧为君贤圣，是因为他把天下让给了许由，许由没有接受，因此尧有了让天下的美名而实际上并没有失去天下。如果现在您把国家让给子之，子之一定不敢接受，这就表明您和尧有同样的高尚品德。”于是燕王哙进一步上当，燕国大事皆付于子之；另一位大臣见燕王哙上当，又对他说：“大禹举荐了伯益，却任用启的臣子当官吏。等到大禹年老时，又认为启不足以担当治理天下的重任，把君位传给了伯益。不久，启就和他的同党攻打伯益，夺走了君位。天下人都说大禹名义上是把天下传给了伯益，而实际上接着又让启自己夺了回去。现在大王说是把国家托付给了子之，但官吏却没有一个不是太子的臣子，这正是名义上把国家托付给子之，实际上还是由太子执政啊”，吃了迷魂药的燕王哙竟把俸禄三百石以上的官吏的印信收起来，将江山真的“禅让”给了丞相子之。<br />
<br />
&nbsp; &nbsp; 这是中国历史上唯一一次真诚的禅让，虽说是子之设下的圈套，但燕王哙是出于真心的。大概他确实看中了子之的大才，确实有“天下为公”的思想，一心想做尧舜。燕王哙视权力为粪土，万万没有想到他看错了人。禅让的结果，是引起了燕国的混乱，将军市被和太子平造了反，大儒孟子不但没有推崇燕王哙的“禅让义举”，反而鼓动齐王借机讨伐燕国。最后禅让者燕王哙与被禅让者丞相子之皆在动乱中死于非命。<br />
<br />
&nbsp; &nbsp; 只是到了新中国建立后，国家领导人才彻底消灭了几千年的“家天下”，走向共和的新中国真正继承了“禅让”的衣钵。</div>

							
							
							
							
