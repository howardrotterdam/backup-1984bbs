不少粉丝从深圳和周边市区赶来。 摄影：南都记者 刘媚<br />
<br />
Q：郭敬明，你有没有历史感和责任感？<br />
A：这个问题太深，我现在关注的是通过自己的活动，来吸引更多的青少年把时间放在读书上而不是电脑游戏上。<br />
<br />
南都讯 记者张鹏 昨日下午“长安娇子计划”青少年主题教育系列活动启动仪式在长安镇图书馆举行。80后著名作家郭敬明做客“长安娇子计划”系列活动中的“娇子学堂”首堂讲座，做了“80后的文学梦想与选择”演讲。讲座吸引了周边地区近2000名青少年参加。期间，郭敬明跟青少年进行了互动，活动结束后还进行了签名售书活动。<br />
<br />
粉丝提前3小时候场<br />
<br />
作为“长安娇子计划”系列活动之一的“娇子学堂”首堂讲座便邀请了80后著名作家郭敬明做客，主讲“80后的文学梦想与选择”。<br />
<br />
演讲安排在昨日下午3点，但中午12点，郭敬明的粉丝们便冒着酷暑来到长安图书馆等待。记者在现场看到，不少粉丝是从深圳和周边市区赶来的。主办方安排的现场售书台，500本郭敬明的书，不到两小时便销售一空。主办方为了照顾未能得到门票的青少年朋友，在图书馆大厅了安放了投影仪，现场直播郭敬明的演讲。<br />
<br />
身材瘦小，打扮时尚的郭敬明准时出现在“娇子学堂”。他的出现，引爆了现场的气氛。在访谈中，郭敬明简单回顾了自己出道十年的历程。<br />
<br />
“文学是一种生计。”郭敬明说，能走上今天的道路是始料未及的。从小就热爱写作，很小就开始给杂志社投稿。慢慢地，郭敬明发现写作不仅可以满足自己的爱好，还可以赚钱糊口。“所以我觉得写作对于自己是一件快乐的事情，既是自己爱好的，又可以养活自己和家人。”郭敬明说。<br />
<br />
“网络文学大多是垃圾”<br />
<br />
郭敬明还和长安的70、80、90三个年代的文学爱好者进行了交流。当被问及自己对自己作品价值的看法时，郭敬明表示，文学是多样性的，不应该由统一的标准来判断文学作品的价值，“我写作品从来不看大众，只写自己喜欢写的东西！”郭敬明说。在谈及自己读书的情况是，郭敬明说：“一本书，只要能够让你坚持读完，这本书就是好书，如果让你没有兴趣读完，那再大的历史价值思想价值都没有。”<br />
<br />
“网络文学大多都是垃圾。”交流中，郭敬明还谈了自己对于网络文学的看法，他称，他很赞同网络文学99%都是垃圾的观点。郭敬明认为，造成这样结果的原因在于，网络文学表达的成本太低。网络文学只是一种载体，而不应当成为文学创作的一种工具。“古代的竹简，古人每刻一个字都很费劲，所以古人的文字都很精练，而现在网络文学可以一天写几万字，根本不会对文字进行提炼。”<br />
<br />
记者看到，来到现场并不全是青少年，一些年纪稍长的人也出现在现场。一名中年男子提问“郭敬明，你有没有历史感和责任感？”郭敬明笑了笑说“这个问题太深，我现在关注的是通过自己的活动，来吸引更多的青少年把时间放在读书上而不是电脑游戏上。”<br />
<br />
在谈及韩寒近期推出的《独唱团》杂志时，郭敬明简短回答：“我没看过《独唱团》，他的封面很不吸引人。”<br />
