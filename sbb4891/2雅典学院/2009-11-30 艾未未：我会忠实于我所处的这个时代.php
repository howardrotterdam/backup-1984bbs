<img src="http://i45.tinypic.com/nxp1eg.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
艾未未：许多事情很荒诞<br />
<br />
<strong>去年四川地震后，在舆论对豆腐渣工程的责问声中，中国艺术家艾未未呼吁当局调查并公布死亡学生人数，并发起搜集遇难学生资料的民间行动。今年8月艾未未在成都遭到公安拘禁和殴打。10天前，他的银行账户受到警方调查。11月27日，数十名各界人士在互联网上发表《声援艾未未的网络呼吁》，对艾未未的人身安全表示关注，要求停止一切对他的威胁与迫害，并呼吁关心和支持他的人们以各种可能的方式进行声援。呼吁书发表的当天德国之声记者与正在台北的艾未未通了电话。</strong><br />
 <br />
<br />
<strong>德国之声</strong>：艾未未您好。以北京部分文化和经济界人士为主的一批各界人士在网上发表了《声援艾未未的网络呼吁》，认为您的人身安全受到威胁，要求当局停止对您的威胁和迫害。他们这种担心您觉得有道理吗？您自己的感受是怎样的？<br />
<br />
<strong>艾未未</strong>：我觉得中国目前的情况是对很多个人，特别是对维护自己权利的个人构成了很大的威胁。我以前没有太意识到这个问题，直到我为谭作人的案子作证的时候，遇到了警方的阻挠和暴力。这个时候你会意识到事情是相当的严重，一个国家的司法机构竟然公然地违法。之后我们又通过合法的渠道进行申诉，在3个月后得到警方的正式的回答是，他们没有做错任何事，也不存在暴力的问题，他们的执法是合法的。<br />
<br />
这个时候你会意识到，这是一个政府和国家行为，它不仅是个人的一种暴力，而是政府和国家无视事实，公然地歪曲，是一种国家暴力。在这样一种意识的指导下，对公民来说什么事情都可能发生，因为这个系统是保护这种暴力行为的。<br />
<br />
<strong>德国之声</strong>：您刚才提到了谭作人的案子。谭作人也是在四川地震后参与维权，要求调查豆腐渣工程以及死难学生的人数。您认为这是不是您和您的同伴受到人身威胁的主要原因呢？<br />
<br />
<strong>艾未未</strong>：我认为这是原因之一，但并不一定是主要原因。因为在中国社会可以看到，很多并没有明确的维权意识的人仍然受到了不同形式的暴力，受到暴力之后他们才变成了维权人士。今天我们可以看到，很多人站出来，开始为他人的不幸提出支持或呼吁，正是由于暴力的普遍存在，以及它会威胁到每一个公民生存的最基本的前提。<br />
<br />
<strong>德国之声</strong>：对您表示声援的这份网络呼吁，里面提到的让他们为您感到担忧的一个直接原因是，11月19日公安部派人到中国银行调查了您的账户，理由是&quot;涉嫌诈骗&quot;。有这样一回事吗？<br />
<br />
<strong>艾未未</strong>：当然，这是他们做的一件我觉得不那么明智的事。显然，政府对一个公民要进行迫害的话，是可以找到各种理由的。很明显，对这种理由将会引起的反弹以及社会上由此产生的公众舆论，他们已经无所顾忌。这样一个社会将走向何处？它如何自视其统治和基本的管理水平，这些都是让人感到荒诞之处。当然这种荒诞是以每个个人付出更大的代价来显现的。<br />
<br />
<strong>德国之声</strong>：警方在调查您的账户之后有没有给您一个说法或是结果？<br />
<br />
<strong>艾未未</strong>：没有。我无法猜测他们要做什么。当然他们的调查可能还需要很长时间，但是我不知道他们想做什么。我能想象的是，他们希望对我进行某种法律上的限制或是制裁。直接原因很大程度上就是我为社会上那些不幸的人们以及不公正的现象做出了我个人的辩护和抗争。<br />
<br />
<strong>德国之声</strong>：就是说您自己也觉得可能会面对一件司法诉讼的官司？<br />
<br />
<strong>艾未未</strong>：那要看他们调查的结果，还有他们对这种做法的利弊的权衡吧。我自己无法猜测他们想要作什么。<br />
<br />
<strong>德国之声</strong>：对那些在网络上发表呼吁，对您表示声援的人士，您想说些什么？<br />
<br />
<strong>艾未未</strong>：我很感谢每一个人为我，其实不只是为我，也是为了他们心目中的公平和正义付诸的行动。我觉得这个社会要发生任何变化，必然是每一个的觉悟，而不是凭几个人的努力。我们可以看到在今天的中国有这种趋势，很多人会不顾一切地站出来，这就是一个社会发生变化的最大的契机。<br />
<br />
<strong>德国之声</strong>：您现在的处境的确可以说有一定的危险，您将如何面对这种情况，有什么打算吗？<br />
<br />
<strong>艾未未</strong>：我生活在这样一个时代，我会忠实于我所处的这个时代，我会面对所有我必须面对的问题。所以我并不会因为别人对我的威胁而改变我个人的行为方式。<br />
