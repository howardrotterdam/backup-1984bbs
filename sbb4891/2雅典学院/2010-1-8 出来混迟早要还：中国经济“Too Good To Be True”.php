													<h2>出来混迟早要还：中国经济“Too Good To Be True”</h2>
						
						
																											<div id="postmessage_420183" class="t_msgfont"><img src="http://graphics8.nytimes.com/images/2010/01/08/business/08chanos-ready/popup.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
SHANGHAI ― James S. Chanos built one of the largest fortunes on Wall Street by foreseeing the collapse of Enron and other highflying companies whose stories were too good to be true.<br />
<br />
Now Mr. Chanos, a wealthy hedge fund investor, is working to bust the myth of the biggest conglomerate of all: China Inc.<br />
<br />
As most of the world bets on China to help lift the global economy out of recession, <strong>Mr. Chanos is warning that China’s hyperstimulated economy is headed for a crash, rather than the sustained boom that most economists predict.</strong> Its surging real estate sector, buoyed by a flood of speculative capital, looks like “Dubai times 1,000 ― or worse,” he frets. He even suspects that <strong>Beijing is cooking its books, faking, among other things, its eye-popping growth rates of more than 8 percent.</strong><br />
<br />
“Bubbles are best identified by credit excesses, not valuation excesses,” he said in a recent appearance on CNBC. “And there’s no bigger credit excess than in China.” He is planning a speech later this month at the University of Oxford to drive home his point.<br />
<br />
As America’s pre-eminent short-seller ― he bets big money that companies’ strategies will fail ― Mr. Chanos’s narrative runs counter to the prevailing wisdom on China. Most economists and governments expect Chinese growth momentum to continue this year, buoyed by what remains of a $586 billion government stimulus program that began last year, meant to lift exports and consumption among Chinese consumers.<br />
<br />
Still, betting against China will not be easy. Because foreigners are restricted from investing in stocks listed inside China, Mr. Chanos has said he is searching for other ways to make his bets, including focusing on construction- and infrastructure-related companies that sell cement, coal, steel and iron ore.<br />
<br />
Mr. Chanos, 51, whose hedge fund, Kynikos Associates, based in New York, has $6 billion under management, is hardly the only skeptic on China. But he is certainly the most prominent and vocal.<br />
<br />
For all his record of prescience ― in addition to predicting Enron’s demise, he also spotted the looming problems of Tyco International, the Boston Market restaurant chain and, more recently, home builders and some of the world’s biggest banks ― his detractors say that he knows little or nothing about China or its economy and that his bearish calls should be ignored.<br />
<br />
“I find it interesting that people who couldn’t spell China 10 years ago are now experts on China,” said Jim Rogers, who co-founded the Quantum Fund with George Soros and now lives in Singapore. “China is not in a bubble.”<br />
<br />
Colleagues acknowledge that Mr. Chanos began studying China’s economy in earnest only last summer and sent out e-mail messages seeking expert opinion.<br />
<br />
But he is tagging along with the bears, who see mounting evidence that China’s stimulus package and aggressive bank lending are creating artificial demand, raising the risk of a wave of nonperforming loans.<br />
<br />
“In China, he seems to see the excesses, to the third and fourth power, that he’s been tilting against all these decades,” said Jim Grant, a longtime friend and the editor of Grant’s Interest Rate Observer, who is also bearish on China. “He homes in on the excesses of the markets and profits from them. That’s been his stock and trade.”<br />
<br />
Mr. Chanos declined to be interviewed, citing his continuing research on China. But he has already been spreading the view that the China miracle is blinding investors to the risk that the country is producing far too much.<br />
<br />
<strong>“The Chinese,” he warned in an interview in November with Politico.com, “are in danger of producing huge quantities of goods and products that they will be unable to sell.”</strong><br />
<br />
In December, he appeared on CNBC to discuss how he had already begun taking short positions, hoping to profit from a China collapse.<br />
<br />
In recent months, a growing number of analysts, and some Chinese officials, have also warned that asset bubbles might emerge in China.<br />
<br />
The nation’s huge stimulus program and record bank lending, estimated to have doubled last year from 2008, pumped billions of dollars into the economy, reigniting growth.<br />
<br />
But many analysts now say that money, along with huge foreign inflows of “speculative capital,” has been funneled into the stock and real estate markets.<br />
<br />
A result, they say, has been soaring prices and a resumption of the building boom that was under way in early 2008 ― one that Mr. Chanos and others have called wasteful and overdone.<br />
<br />
“It’s going to be a bust,” said Gordon G. Chang, whose book, “The Coming Collapse of China” (Random House), warned in 2001 of such a crash.<br />
<br />
Friends and colleagues say Mr. Chanos is comfortable betting against the crowd ― even if that crowd includes the likes of Warren E. Buffett and Wilbur L. Ross Jr., two other towering figures of the investment world.<br />
<br />
A contrarian by nature, Mr. Chanos researches companies, pores over public filings to sift out clues to fraud and deceptive accounting, and then decides whether a stock is overvalued and ready for a fall. He has a staff of 26 in the firm’s offices in New York and London, searching for other China-related information.<br />
<br />
“His record is impressive,” said Byron R. Wien, vice chairman of Blackstone Advisory Services. “He’s no fly-by-night charlatan. And I’m bullish on China.”<br />
<br />
Mr. Chanos grew up in Milwaukee, one of three sons born to the owners of a chain of dry cleaners. At Yale, he was a pre-med student before switching to economics because of what he described as a passionate interest in the way markets operate.<br />
<br />
His guiding philosophy was discovered in a book called “The Contrarian Investor,” according to an account of his life in “The Smartest Guys in the Room,” a book that chronicled Enron’s rise and downfall.<br />
<br />
After college, he went to Wall Street, where he worked at a series of brokerage houses before starting his own firm in 1985, out of what he later said was frustration with the way Wall Street brokers promoted stocks.<br />
<br />
At Kynikos Associates, he created a firm focused on betting on falling stock prices. His theories are summed up in testimony he gave to the House Committee on Energy and Commerce in 2002, after the Enron debacle. His firm, he said, looks for companies that appear to have overstated earnings, like Enron; were victims of a flawed business plan, like many Internet firms; or have been engaged in “outright fraud.”<br />
<br />
That short-sellers are held in low regard by some on Wall Street, as well as Main Street, has long troubled him.<br />
<br />
Short-sellers were blamed for intensifying market sell-offs in the fall 2008, before the practice was temporarily banned. Regulators are now trying to decide whether to restrict the practice.<br />
<br />
Mr. Chanos often responds to critics of short-selling by pointing to the critical role they played in identifying problems at Enron, Boston Market and other “financial disasters” over the years.<br />
<br />
“They are often the ones wearing the white hats when it comes to looking for and identifying the bad guys,” he has said.<br />
