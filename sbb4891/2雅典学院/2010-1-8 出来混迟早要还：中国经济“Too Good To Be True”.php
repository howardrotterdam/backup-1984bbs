12月他做客CNBC，探讨如何在中国的经济崩溃中获利。<br />
近几个月，越来越多的研究人员以及一些中国政府的官员也警告在中国可能会出现资产泡沫。<br />
据估计，中国庞大的刺激方案and record bank lending在去年翻了一番（此句不解……）, 向经济系统中注入了大量资本,为中国的经济增长重新点火助推.<br />
