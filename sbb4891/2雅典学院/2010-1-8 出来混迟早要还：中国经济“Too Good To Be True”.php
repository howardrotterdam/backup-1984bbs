													<h2>美专家警告中国经济在滑向崩溃</h2>
						
						
																											<div id="postmessage_421831" class="t_msgfont"><a href="http://www1.voanews.com/chinese/news/china/Contrarian-Investor-See-Economic-Crash-in-China-20100108-81031817.html" target="_blank">http://www1.voanews.com/chinese/ ... 00108-81031817.html</a><br />
<br />
2010年 1月 08日<br />
记者: 袁野 | 华盛顿<br />
<br />
一位颇有影响力的华尔街对冲基金经理说，中国经济正在滑向崩溃，而不是多数专家所预测的持续繁荣。<br />
<br />
华尔街对冲基金Kynikos Associates创始人兼总裁詹姆斯・查诺斯(James Chanos)在2000年成功预测了安然公司的破产。10年后，他把目光转向了一个更为庞大的经济体。<br />
<br />
*信贷过度问题更加严重*<br />
<br />
查诺斯说，判断泡沫存在的最好证据是过度信贷，而不是过度估值。目前世界上没有什么地方比中国的信贷过度问题更加严重。他警告说，中国过度刺激的经济正在走向崩溃，而不是很多经济学家预测的持续繁荣。<br />
<br />
查诺斯最近在接受美国财经媒体CNBC采访的时候说，中国经济的指令性成分和天量信贷结合在一起，造成资源和产能的错误配置，其结果可能是灾难性的。他说，<br />
<br />
“我们必须记住，上一个有如此之高的经济增速的计划经济体是前苏联。苏联的情况是资源的错误配置造成了很多低效率的工程，比如漏水的大坝和事故频繁的核电站。中国现在走在同一个方向。我们看到越来越多的迹象显示很多工业生产设施一盖好就处于闲置状态。”<br />
<br />
查诺斯的观点和很多对中国经济持悲观看法的人如出一辙。他相信，中国庞大的财政刺激和持续宽松的信贷政策吹大了资产泡沫，人为制造出需求旺盛的假象。<br />
<br />
他在去年年底接受美国时事刊物Politico采访时说：“中国人现在的危险是生产出过多卖不出去的产品。”<br />
<br />
查诺斯2000年从安然公司的记帐方式中看出破绽，并从2000年8月到次年1月通过卖空安然大赚了一笔。此后，他对泰科国际(Tyco International)、波士顿市场(Boston Market)连锁店、以及最近的房市危机和大型金融机构困境的预测进一步奠定了他先知先觉的名声。<br />
<br />
不过，也有人说，查诺斯对中国经济的了解远远不如他对西方金融市场那么了如指掌。和索罗斯共同创办量子基金的国际投资大师吉姆・罗杰斯在接受&lt;&lt;纽约时报&gt;&gt;采访时对查诺斯唱衰中国感到不屑一顾。他说：“有趣的是，10年前不会拼写‘中国’的人现在成了中国问题专家。”<br />
<br />
*多数人看好中国经济增长*<br />
<br />
乔治城大学公共政策学院教授盖宝德(Albert Keidel)认为，中国消费需求的恢复是真实的。消费在出口下滑时候为中国经济的持续增长提供了必要支撑。他说，<br />
<br />
“我们从家庭需求、政府需求、投资和出口等几个方面研究了过去几个季度中国的经济走势。2009年前三个季度，尽管净出口一度降到零的水平，但中国经济在这三个季度仍然增长了将近百分之10。分析显示，这主要是因为内需在二季度和三季度格外强劲。”<br />
<br />
