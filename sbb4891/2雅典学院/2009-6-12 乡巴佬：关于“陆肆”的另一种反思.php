													<h2>乡巴佬：关于“陆肆”的另一种反思</h2>
						
						
																											<div id="postmessage_120066" class="t_msgfont">关于“陆肆”的另一种反思--乡巴佬<br />
&nbsp; &nbsp;&nbsp;&nbsp;64的结论就不多说了，一场惨烈的民主运动。因中共中央开枪镇压而结束。迄今20周年。如果有转世说，那些罹难者今天也风华正茂了。但他们20年前给我们留下的东西，我们是否都找到了呢？如果我们不对那场悲剧做全面的反思，今天的我们能否对得起他们在20年前抛洒的热血呢？我说一下我的看法和反思，政府方面的就不多说了，大家有目共睹。主要说下学生方面和民众方面。<br />
&nbsp; &nbsp;&nbsp;&nbsp;64归根结底还是由于执政党官员腐败引起的。导火线是Huyaobang的逝世。Huyaobang是共产党里少有的开明派领导人。他的不正常下台和逝世引起了学生们压抑许久的怒火。4月16日学生开始在校园进行悼念活动。17日开始在天安门聚集。18日凌晨学生们要求人大常委接见并提出七点要求：<br />
一，重新评价Huyaobang同志的是非功过，肯定其民主、自由、宽松、和谐的观点；<br />
二，彻底否定清除精神污染和反对资产阶级自由化，对蒙受不白之冤的知识分子给予平反；<br />
三，国家领导人及其家属年薪及一切形式的收入向人民公开，反对贪官污吏；<br />
四，允许民间办报，解除报禁，实行言论自由；<br />
五，增加教育经费，提高知识分子待遇；<br />
六，取消北京市政府制定的关于游行示威的「十条」规定；<br />
七，要求政府领导人就政府失误向全国人民作出公开检讨，并通过民主形式对部份领导实行改选。<br />
&nbsp; &nbsp;&nbsp; &nbsp;上午8时，政府作出回应，这表明学生抗议已经引起高层的重视。国务院派出一名官员接见学生代表郭海峰、王丹，期间学生继续静坐，声援人数不断增加，到晚上9点，广场已经聚集约2万名学生。有警察与民众在新华门对峙 。<br />
&nbsp; &nbsp;&nbsp; &nbsp;到了19日，学生开始冲击新华门。据Zhaoziyang回忆，当时只是少量的学生代表要求进中南海吊唁送花圈。后面有大量群众看热闹，学生在前面提要求啊，要求见什幺人啊，后面的人往前涌，往前推，秩序有点乱。在我看来，这时候学生应该果断停止，暂时离开。因为后面看热闹的人在拿前面学生的生命开玩笑！面对持枪军警，暴力冲击国家机关，实在危险。后来学生自己组织了纠察队，把学生和围观的人隔开，才化解危机。<br />
&nbsp; &nbsp;&nbsp; &nbsp;4月20日，学生们建立了领导组织，提出“非暴力”、“不抵抗”抗争，以罢课争取民主。这说明学生们这时候很理性。接着官方媒体对抗议活动进行了批评。学生们提出了激进的口号标语：“流血不要紧，自由最可贵”、“用战斗迎接五四”、“北京冲击中南海，俺们咋办？ ”试问，如果当时学生打出：“要民主，要自由，打不还手骂不还口。”这样的标语，又会是怎幺样一个景象呢？<br />
&nbsp; &nbsp;&nbsp; &nbsp; 4月22日。西安、长沙出现后来被称为“四二二事件”的严重骚乱。据报晚上5时起，西安有人焚烧二辆汽轮和五间房子，逃走时又有人在西华门、钟楼附近焚车和抢劫商店，西安公安部门大举缉捕270人，其中大、中、小学生占72人，西安初审164人后，释放了106人，但大学生比例不详。长沙亦有20家商店被抢劫，96人被拘捕，当中3人为大学生。谁说64没有流氓？破坏分子利用机会大肆违法犯罪，给社会安定带来重大影响。也给抗议活动蒙上了阴影。<br />
&nbsp; &nbsp;&nbsp; &nbsp;上午，Huyaobang的追悼会在大会堂举行。秩序良好。追悼会下午结束后，学生虽然满意Zhaoziyang极大赞美Huyaobang的悼词，但接近一昼夜的静坐毫无结果，情绪激愤。学生担心入夜出事，决定撤出广场，并“通电全国，无限期罢课”。 这时候学生又表现出了极大的克制和理性。<br />
&nbsp; &nbsp;&nbsp; &nbsp;4月26日，政府通过官方媒体把抗议活动定性为反革命暴乱。这是没有道理的，最起码北京的学生还没有表现出极端不理性的举动。于是矛盾被激化。当晚，长春、上海、天津、杭州、南京、西安、长沙、合肥等城市发生规模不同的示威，抗议社论，其中以长春规模最大。当晚吉林大学等校约3000名学生涌到省委门前，强烈要求同省委领导对话，并表明因为听到运动被定性为“动乱”才上街。<br />
&nbsp; &nbsp;&nbsp;&nbsp;学生召开记者会反驳“学生颠覆中共”的言论，并再度要求对话 。4月27日，北京10万人上街示威[33]，虽然政府明令禁止游行，但警方并未强硬阻挠，学生的标语是“和平请愿，不是动乱”、“打倒官僚”，以至“拥护共产党”，“拥护社会主义，”游行秩序良好，最终和平落幕。这时候学生表现出了智慧和策略。这是64前期很有代表性的现象。所以有人说64学生盲目激进，以抗议之名大肆破坏，在前期是站不住脚的。<br />
&nbsp; &nbsp;&nbsp; &nbsp;4月28日，政府又发布社论，语调比较温和。指出眼下要维持稳定，确保改革大局。如果动乱则是民族灾难。<br />
&nbsp; &nbsp;&nbsp; &nbsp;4月29号，政府在学联的协调下与45名学生代表对话。肯定绝大部分学生是爱国的。承认“党风不正和各种腐败现象有目共睹。”这时候就显示出政府已经作出让步。学生的抗议已经取得初步的结果。但学生对于这个结果看法迥异。有人认为对话十分成功。有人认为对话没有取得实质成果，并对学生代表的代表权提出质疑。这就表现出了学生的不够成熟。哪里有在对话之后才质疑代表权的道理？在对话之前就应该选出代表大家意愿的人选。不能因为代表没有达成自己的意愿就否定他的资格。这样做只能给政府以口实，认定学生胡搅蛮缠没有对话的诚意。学生们应该意识到把一个专制政府改变成民主政府不是一天可以达成的事情。应该珍惜自己取得的哪怕是不起眼的小成果。涓涓细流汇成大海。<br />
&nbsp; &nbsp;&nbsp; &nbsp;下午，北大的王丹和北师大的吾而开西等人召开记者会批评对话像开记者会。职责政府没有诚意。要求重新对话。<br />
&nbsp; &nbsp;&nbsp; &nbsp;30日，三成的学生回校复课，北京市官员与北京17所高校的学生代表进行对话。<br />
&nbsp; &nbsp;&nbsp; &nbsp;5月1日，学生组织“北京高校学生自治联和委员会”（高自联）召开记者会，再次提七点要求，包括正确评价Huyaobang；公正评价学生运动；惩处日前新华门殴打学生的人；反贪污、反腐败；并且尽快起草新的《新闻法》，支持香港报人徐四民回大陆办报的要求；提高教育经费；由全国政协出组成专家论证小组，检讨政府政策失误等。<br />
&nbsp; &nbsp;&nbsp; &nbsp;5月2日，上海学生抗议市政府关闭同情学生运动的一家报社举行游行。高自联则选出请愿代表，由王超华、王丹、郑旭光带领学生向中共中央、全国人大和国务院的信访部提交声明，提出要由普选的学生代表与政府对话，要求允许旁听、有电视直播，双方代表要有相同的时间发言，且保障学生代表的安全。当天，新华社罕有地刊载学生声明的全部内容。在声明中，学生特别提到：“如果5月3日中午12：00以前我们得不到答覆，我们将保留在5月4日继续请愿的权利。”这份声明随即送呈中央政治局各位常委，中共中央政治局决定5月3日派出袁木举行中外记者招待会，反击学生言论。袁木在会上批评学生要求像“最后通牒”，无法接受，并指学生代表已然犯法，政府必须追究。<br />
&nbsp; &nbsp;&nbsp;&nbsp;于是矛盾激化了。在我看来，矛盾的激化双方都有原因。但主要原因还是在于部分学生急于求成，对政府的底线认识不够。<br />
&nbsp; &nbsp;&nbsp;&nbsp;5月4号。总书记Zhaoziyang发表了54讲话。一方面强调稳定的重要性，同时也强调学生的要求与共产党是一致的，在学生之间颇获好评，北高联宣布，从次日起，结束罢课。<br />
&nbsp; &nbsp;&nbsp;&nbsp;5月5日，80%北京高校学生已复课，但北京大学和北京师范大学学生仍有一半学生罢课，有些学生认为中央对学生运动的定性不改变就不应该复课，当晚北大和北师大“学生自治联合会”宣布次日将继续罢课。<br />
&nbsp; &nbsp;&nbsp; &nbsp;事情发展到这个时候就有点匪夷所思了，4号高自联宣布结束罢课，人民日报也做了宣传，仅仅过了一天5号就宣布继续罢课。绑架，我这幺认为。这时候大部分的学生已经认为没有继续抗议的必要了。但部分学生，特别是学生领袖们担心政府报复，学校有看法，将来不好找工作。要求继续游行。我认为这是一种绑架，学生们口号里喊着自由，这时候却没有理解自由的真正含义。没有尊重大部分同学复课的自由。这不能不说是一种遗憾。<br />
&nbsp; &nbsp;&nbsp; &nbsp; 5月13日，柴玲等人发起绝食运动。由几百名学生围绕在纪念碑周围绝食抗议。将学潮推向了最高潮。同时也把矛盾激化到最高潮。他们向全国发布绝食书。得到了市民，机关单位，包括武警部队在内的很多人们的声援，各地开展募捐，抢救。铁路开始默许外地学生免费乘车进京。20多个城市的学生上街游行声援。他们极感情色彩的煽动其他同学加入绝食。局面开始朝着不可控的方向发展。<br />
&nbsp; &nbsp;&nbsp; &nbsp;政府也意识到了危险，约请知识分子斡旋，严家其、戴晴、刘晓波等到广场劝说学生停止绝食，未果。5月14日凌晨2点半，李铁映、李锡铭、陈希同等到天安门广场劝学生复课；下午16时，李铁映、阎明复、尉建行等再与30余所高校学生对话，亦未果。<br />
&nbsp; &nbsp;&nbsp; &nbsp;5月15日，学生发动北京大游行，长安街交通瘫痪，地铁前门站封闭，游行口号开始针对Zhaoziyang、木子月月鸟和当时教育部长何东昌，舆论逐渐同情学生。作为政府喉舌的中央电视台每天转播天安门广场的情况，人民日报也不断报道。政府当日与北京高校学生对话代表团对话。未果。由于学生绝食抗议，原定在广场举行的外事活动取消。<br />
&nbsp; &nbsp;&nbsp; &nbsp;5月16日，抗议活动持续到今天满月。数十万北京各界群众涌向天安门广场，声援绝食学生。阎明复在学生前说：“改革需要你们进行下去……你们要爱护自己，等待正义的裁判的这一天就要到来了。我请求你们，我可以和你们一起静坐，请求你们能够爱惜自己。”然而学生未有理会劝告。<br />
&nbsp; &nbsp;&nbsp; &nbsp;这一天，Zhaoziyang在会见苏联领导人时透露出了邓小平是中共实际领袖的事实。于是学生的矛头开始转向邓小平。开始打出针对邓小平的标语，例如“小平糊涂”、“老眼昏花少而无能”、“八十五岁，尚能饭否”等等，而参加游行的人之中，亦有人打起政府部门的旗号，外交部、邮电部、国家体委、中宣部、国家教委、国中央、中央党校、求是杂志等等单位，都有干部参加游行。 <br />
&nbsp; &nbsp;&nbsp; &nbsp;至此，可以说事情已经开始失控。<br />
&nbsp; &nbsp;&nbsp;&nbsp;17号，严家其、包遵信等发表《五一七宣言》，进一步攻击邓小平是「一位没有皇帝头衔的皇帝，一位年迈昏庸的独裁者」，发动知识分子签名。<br />
学潮发展到现在，作为学生思想启蒙者，教育者，引导者的知识分子开始走向前台。<br />
&nbsp; &nbsp;&nbsp;&nbsp;17日在邓小平家召开的会议上，中共保守势力开始向以Zhaoziyang为代表的改革派发起总攻。指责学潮之所以不能平息是由于Zhaoziyang的54讲话。会议决定调派军队进京戒严。成立以木子月月鸟为首的领导小组实施。Zhaoziyang至此实际上已经失去权力。保守派完全掌握了国家机器。<br />
&nbsp; &nbsp; 19日凌晨，Zhaoziyang到天安门广场，几乎是苦苦哀求学生们撤离。含泪向学生道歉、劝学生撤退、并承诺政府不会秋后算账。但学生虽然很感动，却没有撤离。放弃了戒严前离场的机会。作为执政党的总书记，政府总理的Zhaoziyang到广场哀求学生撤离，并保证不秋后算账。这时候学生们完全有理由见好就收，暂时退让，以求转机。但是学生这时候已经让冲动和激进代替了理性。<br />
&nbsp; &nbsp;&nbsp; &nbsp; 5月20日，学潮爆发一个月又四天，北京宣布戒严。北京附近的军队开始开进，当时市郊有零星流血冲突，但军队严守“打不还手，骂不还口”的命令，冲突并不严重，也有不少民众向军人送水送粮、慰问军人，劝他们放下武器。一些士兵流泪，一些显得不知所措，事后据称有军长级干部因拒绝执行戒严令而被撤职。<br />
这表明戒严初期，中共中央还保持了克制。如果学生在戒严后就撤离。最起码可以避免大规模流血。但是学生们仍然没有撤离。于是学潮朝着“你死我活”这样的方向发展了。局势完全恶化。<br />
&nbsp; &nbsp;&nbsp;&nbsp;5月26日，新华社放出消息。中共元老和常委们开会。将活动定性为：极少数人制造复杂局面，煽动学生反对共产党。<br />
到这里，如果学生们年轻没有预见性的话，那幺作为他们思想启蒙者教育者引导者的知识分子老师们应该可以预见可能马上到来的武力镇压了。可是大部分的知识分子仍然隔岸观火。面对被青春冲动控制着的学生们，面对即将可能到来的屠杀。我们的知识分子无动于衷。他们点燃了学生们的青春热情，却眼睁睁地看着学生们被冲动控制着迎向子弹。<br />
&nbsp; &nbsp; 就在军队缓慢开进的过程中，广场上的学生领袖们却在忙着争夺控制权。大大小小的政变每天都得两三次。面对国内外捐献来的巨额资金还显示出了财务问题。出现学生内部的绑架事件。广场上一片混乱。静坐徒具形式。在“坚持”与“撤退”的问题上争论不休。那些高喊着自由民主的学生领袖居然说出了“谁主张撤退谁就是投降派”。这句话是多幺的熟悉啊，是什幺原因导致了这样顶着文革思想的人做了学生民主运动的领袖呢？令人痛心的是，在他们争权夺利的时候，在他们争论不休的时候。年轻的学生们却一点一点地临近死神。<br />
&nbsp; &nbsp;&nbsp;&nbsp;到了6月，知识分子们才想起孩子们的危险处境。被称为“天安门四君子”的刘晓波、周舵、高新、侯德健等主张撤离的知名人士宣布在天安门绝食72小时，吁学生和政府重新开展和平对话。刘晓波事后指，他们是为了劝退学生才加入绝食，希望令自己在学生之间拥有发言权，料不到这反而坚定了学生的决心；当面对学生高涨的掌声时，他们也觉得心情兴奋，把原先的理智抛诸脑后。<br />
&nbsp; &nbsp;&nbsp; &nbsp;无语啊，我真的无语了。<br />
&nbsp; &nbsp;&nbsp; &nbsp;6月3日星期六，中共中央决定清场。当天下午4时，杨尚昆、木子月月鸟、乔石和姚依林等召开军政会议。决定武力排除障碍。疯狂了，政府抛弃了他们可怜的克制，学生们丢掉了他们宝贵的理智。<br />
&nbsp; &nbsp;&nbsp; &nbsp;晚上10时后，天安门与长安街的形势急转直下。军队入城后遭到市民阻挠，军队开枪，其中木樨地是死伤最多的焦点。<br />
&nbsp; &nbsp;&nbsp; &nbsp;彻底失控了，疯狂了。人们点燃汽车，投击石块，抢夺枪支。人们围堵军队，是因为他们坚信人民的军队不会向人民发射子弹。军人的子弹是用来保护人民的。可是军队开枪了，他们信赖的最可爱的人向他们开枪了，于是人们暴怒了。更加疯狂的去阻拦去报复。善良的人们忘却了一个基本的事实。军队根本没有好坏之分。它只是国家机器的暴力工具，专业的杀人工具。只要命令下达，它可以向任何人开枪。人们不理解，我只是向军队投个石块而已，犯得着开枪吗？可残酷的现实就是这样，只要杀人工具启动，哪怕你只是向它投个纸飞机。它都会熟练地将你击毙。你一个手无寸铁的学生暴力对抗专业的杀人机器。很遗憾，我只能说你在自杀。我这幺说很残忍。但你可以去咒骂那些启动杀人机器的独裁者，可还要问问又是谁让你距离枪口如此之近？<br />
&nbsp; &nbsp;&nbsp; &nbsp; 20年后的今天，我们怀念他们的青春，我们敬佩他们的勇气，我们崇拜他们的理想。我们又能不能在为他们喊冤的时候又反思他们的青涩？时至今日，还有一些顶着文革脑袋独裁脑袋的人高唱着民主自由。独裁者对于民主自由的伤害是显而易见的，他越是独裁就越能激起人们对民主自由的渴望。但那些人呢？那些在枪口逼近的情况下喊着“撤离的是叛徒”的人呢？那些激发了孩子对自由的渴望却不告诉孩子死神临近的人呢？他们是不是对民主自由有着更大的伤害呢？<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;借用《河殇》总撰稿人之一的苏晓康的话结束：抗议永远是可以的。但是要看到力量对比。要负责任。你要认识到，如果让改革大局崩坏，如果让民众白白丧失生命，这是不负责任的。</div>

							
							
							
							
