													<h2>一下“不能照搬西方民主”@f法</h2>
						
						
																											<div id="postmessage_684967" class="t_msgfont">中国至今还不是一个开放的言论平台，使得很多似是而非的说法不能得到充分的讨论。“中国不能照搬西方民主”就是一个实例。<br />
<br />
首先，我觉得这句话的出发点完全是建立在污蔑民运的立场上，我们主张推动中国的民主化，但从来没有听任何异议分子说过“完全照搬西方民主。”而共产党常常用这句话来证明民运只是“媚外”，从而希望大家忽略“民主”这个概念本身。<br />
<br />
其次，不能完全照搬西方政治制度，是否意味着我们一点也不能照搬？不能百分之百照搬，百分之几是合理的呢？百分之五吗，还是百分之九十五？至少这是值得讨论的吧。<br />
<br />
第三，其实才是最核心的，是否应该定义一下什么是“西方”民主。三权分立吗，议会政治吗，政党政治吗，新闻自由吗，言论自由吗？还是其他什么有特别种族特色的内容。如果民主的表现形式是上述那些，民主的核心价值是自由，是平等，是保障参与，是监督制衡，那么这些东西又有什么不合于中国特有国情或者中国人的种族特性呢？如果这些都不是所谓“不适合照搬的部分”，那么是什么呢？无论这种言论持有者能不能举出有什么是真不适合“照搬”的，我列举出来的这些民主核心价值，表现形式应该总可以接受了吧。<br />
<br />
接受这些核心价值之后，表现形式其实不必完全一致，也谈不上一致，美国搞总统制，法国搞总统总理双首长，德国是责任内阁的总理制；有总统的是共和制，有女王有天皇的是君主立宪；美国搞参众两院，英国是上下两院，德国意大利是单一国会，他们自己之间都不完全一样，在中国搞民主没有什么照搬谁的问题，元首还是可以叫国家主席，国会也许可以改个名字，实行代议权力的国会议员不可以像现在，动辄几千人，完全没有代议的功能。但这些都跟是否照搬西方民主完全没有关系。<br />
<br />
在说“中国不能照搬西方民主”这句话的时候，请说清楚中国不能照搬的西方民主是指什么。共产党居心叵测，有独立思维能力的人不能人云亦云。<br />
<br />
吾尔开希</div>

							
							
							
							
