													<h2>韩寒：散文一篇</h2>
						
						
																											<div id="postmessage_552443" class="t_msgfont">在昨天，我看到了一条新闻，新闻说我候选了时代周刊的两百个影响全球的人物，中国同时入选的还有敏感词，敏感词和敏感词等人。当时我正在我们村里挖笋（我挖的是自己家的），没怎么注意，后来回去一看手机上有不少的短信，问我对此事的态度，我只回复了新京报和南都的两位朋友，其他媒体写的均为凭着对我性格的猜测下的友好想象。我没有想到大家还比较关心，在这里我就做一个统一的回复。<br />
<br />
 <br />
<br />
首先，我非常感叹和惋惜，为什么别人有这样的新闻媒体，当时代周刊弄一个人物榜的评选的时候，能够让全世界其他的国家都起波澜。我多么渴望我们中国也能有类似的一个新闻媒体，当他评选人物的时候，在全世界也引起关注。我们不能说这样的一个媒体完全公正，但是它是有完全的公信力的，我多么渴望我们国家也有。可惜我们并没有。不是说我们的媒体人要比其他地方的媒体人差，而是因为一些……原因，这些原因众所周知，点到为止，多说必死，死后鞭尸。<br />
<br />
 <br />
<br />
我经常自问自己，我为这个充满着敏感词的社会做出了什么贡献，可能到最后我只贡献了一个以我的名字命名的敏感词而已。我天天睡到中午，经常浪费钱买数码产品，还挑食，但好在我也未曾给这个社会增加罪孽和负担，至少迄今为止是这样。我没有辽阔的远见，我唯独只想让相关部门善待文艺和新闻，不要给他们过多的审查以及限制，不要用政府的权利和国家名义去封杀或者污蔑任何一个文艺工作者和新闻从业者，这样的话，不用你们花大价钱，这个国家会自动生产出输出到西方世界的文艺作品和新闻媒体，我们的每一个小小的读者听众观众网民市民国民都能同享荣光。我未必有天赋和能力写出好的东西，但是别人有，但你不要阉人有夸人无。<br />
<br />
 <br />
<br />
电话里记者问我，有一些地方还说你和西方反华势力勾结，我说这个很正常，人家这招用了六十年了，前几十年还有发自内心的，后几十年纯粹是用于泼脏水了。我一个要去西方国家比赛经常因为材料不够齐而差点签证都办不出来的人，还西方势力呢，况且都什么年代了，还勾结不勾结的，这词说出去多难听啊。相信如果有哪位朋友天天监听着我的电话的话，您一定很清楚我究竟是一个怎么样的人，您说呢，电脑前一定会有一位朋友会心一笑的。但我只是奇怪，这些御用笔杆子，怎么几十年都用一个体位，他不烦，对象都烦了。但是，我坚决赞同他们的存在，因为总有正方和反方，总有甲方和乙方，如果我们国家能做到话不投机一拍两散，而不是话不投机把你封杀，那就是我们国家的巨大进步，我们也将为此而努力。<br />
<br />
 <br />
<br />
后来他又发短信问我，那么换句话说，你这个人的观点和言论符合了西方人的价值观，你觉得是么？<br />
<br />
我回消息说，难道不符合中国人的价值观么？<br />
<br />
我相信地球人和外星人也许价值观不一样，但是西方人和东方人，除了生活习惯不一样以外，价值观应该是差不多的，为何一定要争呢。<br />
<br />
 <br />
<br />
最后说回到所谓的影响力，我经常非常的惭愧，我只是一介书生，也许我的文章让人解气，但除此以外又有什么呢，那虚无缥缈的影响力？在中国，影响力往往就是权力，那些翻云覆雨手，那些让你死，让你活，让你不死不活的人，他们才是真正有影响力的人。但是不知道是因为他们怕搜呢还是不经搜，往往在搜索引擎上还搜不到他们。我们只是站在这个舞台上被灯光照着的小人物。但是这个剧场归他们所有，他们可以随时让这个舞台落下帷幕，熄灭灯光，切断电闸，关门放狗，最后狗过天晴，一切都无迹可寻。我只是希望这些人，真正的善待自己的影响力，而我们每一个舞台上的人，甚至能有当年建造这个剧场的人，争取把四面的高墙和灯泡都慢慢拆除，当阳光洒进来的时候，那种光明，将再也没有人能摁灭。</div>

							
							
							
							
