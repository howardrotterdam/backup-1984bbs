													<h2>王炳章：放弃革命的权利就等于放弃了一切</h2>
						
						
																											<div id="postmessage_475081" class="t_msgfont">放弃革命的权利就等于放弃了一切<br />
――南斯拉夫的革命昭示了我们什么？<br />
王炳章<br />
<br />
米洛舍维奇倒台了，南斯拉夫的民主革命取得了胜利。民主，又攻占了一块独裁者的土地。<br />
<br />
我十几年前就结识过南斯拉夫的民运斗士。大概是一九八四年，我去美国首都华盛顿演讲、并会见其他民运朋友。当时，《中国时报》驻华盛顿记者冉亮女士，请我到她家做客。没想到，她的丈夫是一位南斯拉夫人，而且是一位思路清晰的革命者。我们相谈甚欢。我记忆最清楚的，是这位南斯拉夫人士对未来南国政局的准确预测。他预言，而且毫不留有余地地预言，南斯拉夫在铁托去世后会发生解体、爆发内战，共产主义当然也会随风而去。我问他为何如此自信，他说，这是南国内部矛盾的累积使然，任何人也改变不了。他还嘲讽那些美国所谓的南斯拉夫问题专家，说他们对南国问题认识非常肤浅，往往从主观愿望出发而制定对南政策。<br />
<br />
现在，这位南国民运人士的预言已经获得验证。他是一名知识分子，我当年在他身上，却没有闻到那股酸儒的书生气味，反而感触到一种革命家的激情与果断。今天，我们从电视上观看南斯拉夫人民推翻米洛舍维奇的革命，是否也领悟到了其中的那种激情与果断？反观我们中国的八九民运，群众动员的规模远比南国的革命大得多，但我们为什么没有成功呢？这就要对比一下：人家有什么，我们没有什么；人家是怎样做的，我们是怎样做的。然后，我们思考一下，我们是否应当从南国的革命中学些什么。一，攻占国会――夺取政权的意识<br />
<br />
我分分秒秒都在关注南斯拉夫的革命。这几天夜里，我常常爬起好几次，打开电脑，从中英文各种管道捕捉信息。因为我知道――就象我多次讲过的，独裁政权一旦出现危及，其垮台的时间不是以月记，甚至不是以天记，而是以小时记算，其崩溃是异常迅速的。我不想错过任何一个关键的镜头，为的是从中领悟要绝，给未来的中国民主革命提供参考。<br />
<br />
南国革命的转捩点，是群众攻占国会大厦。占领后，立即宣布夺权成功，反对派领袖及时宣布接管权力。也就是说，南国民主运动的领袖和群众，有十分明确的政权意识，目标就是要夺取政权。他们从一开始动员群众，就确定了这个目标，而且从不动摇。我们看到，南斯拉夫的民主派占领国会、宣布夺权成功后，局势一泻千里，首都武装警察纷纷倒戈。俄国政府也不得不迅速宣布承认新的政权。<br />
<br />
中国话说“心想事成”。你心里想到“夺权”，想要“夺权”，才能“夺权”成功。心里没有“夺权”的想法，没有政权意识，夺权的机会来了，你都不知道抓住机会。甚至，把权力放到你的手里，你都不知道权力是什么，你都不会抓住它。<br />
<br />
“夺权”？犬儒式的书生论证者，一听夺权，就吓得浑身发抖。一听要推翻一个即有的政权，就指责这是“胡闹”，这是“过激”，这是“以暴易暴”，这是“野心”等等。一句话，“推翻政权”要不得；“夺权”更是要不得。他们甚至站在民主革命者的前面，举着牌子，挡住革命者的去路，牌子上写着：“告别革命！”<br />
<br />
如果南斯拉夫的革命者们听从这班中国腐儒的话，国会不去占领，权力不去夺取，米洛舍维奇今天依然会稳稳地坐在权力的宝座上，而且，这个独裁者还会津津有味地做下去。“书生论政而悟国”，有道理乎？二，八九民运毫无政权意识<br />
<br />
反观我们八九民运，毫无政权意识。主流运动压根儿就没想到“革命”，更没有去想“夺权”。我在一九八九年五月四日闯关回国，目的就是想在运动中宣传革命的理念。不幸的是，我被拦截在日本东京，日航屈服于中共压力，拒绝载我飞往北京。在解放军三大总部的游行队伍走上街头、支持学生、要求政治改革时，我看革命的时机已到，就拼命联络在天安门广场的民运朋友们，让其带话给学运领袖们，应当迅速占领电台、电视台和人民大会堂。但连我的朋友都说，这样太激进，学生肯定不会听的，学生们恪守“和平、理性、非暴力”，他们不会放弃这个原则。我说，那就去找刘晓波等知识分子，刘晓波回国前，我们在纽约密谈过。但我的民运朋友告诉我，北京知识界的主流也不会接受我的建议，让我暂时不要着急。我怎么不着急？一位美国朋友当时讲，按照政治常识，当时的北京政权已经瘫痪，问题是需要一个替代政权，可惜这个替代政权改出现的时侯没有出现。这样，就给了中共业已瘫痪的政权死灰复然的机会。<br />
<br />
问题就在这里，八九民运时，在六四开枪镇压之前，北京市内，从中央到地方的党政机关大都罢工、瘫痪了，民警也不愿再为共产党效劳了。最高法院、最高检察院、人民日报社、解放军三大总部、高级党校的干部群众，通通上街游行，要求变革。警察都不听话了，这是邓小平调兵勤王的重要原因。可是，旧政权瘫痪了，老百姓拥护什么政权呢？党政干部拥护什么政权呢？军队、警察服从什么政权呢？这个真空，一定要及时填补。没有新的填补，瘫痪的，就会寻机而起。没有新的政权填补，就不能给群众一个希望，一个可以围绕的中心。三，反对运动需要一个凝聚点，需要明确拥护什么<br />
<br />
民主运动、或称反对运动，除了需要知道反对什么之外，更需要明确拥护什么――想要的是什么。拥护什么的问题没解决，就没有一个凝聚点，力量就不可能聚合起来。没有一个微小的尘粒为中心，水蒸汽就不可能凝聚成一片雪花。没有一个核心，雪团就不会滚大。核心不牢，雪球滚大了，也会散掉。<br />
<br />
民主运动要解决凝聚力的问题，至少应包括以下几项内容：共同的信仰；共同拥护什么样的政治主张；共同拥护什么样的政治力量；共同拥护什么样的政治领袖。有时，还需要一个具体的、有拥护价值的“象征”。象共产党打天下的时侯，延安就成了左派青年心目中的“图腾”，拥护、向往的象征，成了一个凝聚点。<br />
<br />
共同拥护的政治主张、政治力量、政治领袖，这次南斯拉夫革命全都具备。尽管这样，在行动中，一个具体的“象征”还是需要的。我们看到，国会大厦，就成了这一象征。国会，象征着最高权力，因而具有极大的象征意义和凝聚力。我观察到，当民主派爬上国会大厦，摇动国旗的时候，群众立即狂欢起来，群众从四面八方拥向国会。国会大厦，不但成了夺权成功的标志，成了凝聚点，也一下成了革命群众誓死保卫的对象。<br />
<br />
中国民运拥护什么的问题，没有很明确的解决。这是需要大家注意的。拥护什么的问题不解决，老百姓就不知改支持什么，改投奔谁，运动就不会滚大。<br />
<br />
八九民运时，天安门广场成了一个象征，一个中心。但它缺乏权力上的象征意义，充其量，当时只是一个“良心”的象征。假如群众攻占了人民大会堂，象征意义和凝聚力就会大上十倍、百倍。因为，它代表着国家的权力回到人民手中。如果成立临时政府、并加上及时而正确的政策宣示，不但对老百姓，就是对军政人员，都会形成巨大的吸引和凝聚力量。四，夺权――公民的天赋人权<br />
<br />
推翻一个暴虐的政权，更替一个不喜欢的政权，或者说，从专制统治者手中夺取政权，这是公民的天赋人权，与生俱来，不可剥夺。有人一听到推翻政府，一听到夺权，脸色就吓得发青。其实，夺权这件事，没什么了不起，应以平常心对待之。我在这个问题上，已经写过很多文章，做过多次演讲。这里，趁着南斯拉夫独裁政权的垮台和民主派的夺权成功，我再把有关要点重复一下。关于革命与夺权，我们至少需要明确以下问题：<br />
<br />
第一，政府是什么？一定需要政府吗？<br />
<br />
第二，政府是怎么来的？谁建的？<br />
<br />
第三，政府的作用是什么？为谁而建？<br />
<br />
第四，政府是谁运作的，什么人有资格运作？<br />
<br />
第五，政府不称职、甚至压迫人民怎么办？可不可以和平地更换政府？<br />
<br />
第六，压迫人民的政府拒绝和平地放权怎么办？<br />
<br />
第七，人民可以进行改良吗？<br />
<br />
第八，放弃革命和夺权的权利，等于什么？<br />
<br />
让我们简单地回答这些问题。<br />
<br />
第一，政府是什么？不要政府行吗？<br />
<br />
是的，很多人不喜欢政府。因此，无政府主义者主张不要政府。完全不要政府行吗？看来不行。比如，发了洪水怎么办？一家一户没有力量治理洪水，必须联合起来，才能治洪。这样，问题就出来了：谁来协调大家？谁来决定治理洪水的方案？治洪需要资金，谁来出这个钱？如何管理这笔钱？等等，一大串问题。因此，大家要共同出钱，这就要交税。管这笔钱，需要财政部门。协调集体力量，需要一个头，一个指挥。这样，必然就会产生政府。处理垃圾问题也是如此。大家都倒垃圾，没有人清理不行。大家必须找一个人、或一批人来专门处理垃圾，这就衍生出来与抗洪一样的问题：政府是必要的，它是处理抗洪、清除垃圾这类“公共利益”所必需的，也是维护社会秩序所必须的。由于政府常常做事做得不好，甚至有了权力以后可能会烂用权力，但是，人们又不能缺少它，因此，民主社会常称政府为“必要的恶魔”。<br />
<br />
第二，政府是谁建立的？来自谁？上面的例子也说明：政府是人民创建的，它来自人民。<br />
<br />
第三，政府的目的何在？政府的目的是处理“公共利益”之事，是造福人民，为民服务。现在，连最专制的独裁者，都在口头上承认，政府来自人民，要为人民服务。只是，他们说一套，做一套。<br />
<br />
第四，政府由谁运作呢？谁有资格来运作呢？是人们挑选的有能力的人来运作，这就产生了选举。如何人民没有推举你、没有委托你来管理政府，这个政府就没有资格管理“公共利益”的事。政治学上说，统治者只有在征得被统治者的同意之后，才能行使统治的权力。<br />
<br />
第五，政府不称职怎么办？甚至，政府压迫人怎么办？答案很简单：让它下去，换一个称职的，换一个不压迫老百姓的。可不可以用和平的、合法的方式更换政府？可以。现在，美国正面临总统大选。共和党的小布什和民主党的高尔，都站了出来，让公民们挑选。如果人民厌倦了民主党的执政，就会把它赶下台，把共和党政府请上来。如果老百姓还喜欢民主党，就会叫小布什走开，继续留着高尔，让他来组织民主党政府。所谓民主选举，说白了，就是老百姓用合法、和平的方式更换政府、换上一个新政府。也就是说，民主制度把更换政府合法化了、和平化了、程序化了。<br />
<br />
第六，压迫人民的政府拒绝和平地交出权力，拒绝和平地更换政府怎么办？人民可以革命，用强迫行的手段叫它走人。因此，美国独立宣言说，人人生而平等，人民有权推翻一个暴虐的政府，必要时，可以使用武力。林肯总统一上台就说，如果它领导的政府是压迫人民的，人民有权起来革命，推倒这个政府。中国文化中，也有人民革命的权力。孟子在论及汤武革命时，就说，人民造反株杀暴虐的纣王，不算“弑君”，乃是除去一个独夫民贼而已。<br />
<br />
因此，推翻政府、更换政府，是公民的天赋人权。革命，是公民的天赋人权，从专制者手中夺取政权，是公民的天赋人权。五，改良是执政者才能做的，而革命却是平民的武器<br />
<br />
一提到革命就脸色发青而高喊“告别革命”的书生们，总是提倡改良，不知写了多少文章诅咒革命、赞扬改良。我不是反对改良，也很希望改良。可是，我要问问那些诅咒革命、将改良绝对化的书生们：你们怎么改良？你们能进行什么改良？你怎样将专制制度改成民主制度？你们手中没有权，能改什么良？这是平民改良派的一个死结。改良是好，很好啊，我很赞成改良，但不要忘记，只有手中有权的当权者，才能进行改良，才能改革。极权专制社会里的所谓改良和改革，说来再简单也不过了-----就是专政者主动放权，主动还政于民，这是改良的实质。比如经济改革，就是共产党放松了一点经济控制权，给了老百姓一点发展私有经济的权力。我们平民百姓怎么改良？怎么改革？我们本来就没权，我们没有使用权力来进行改良的能力！充其量，我们只能呼吁改良，呼吁谁？呼吁执政者，呼吁有权的人，呼吁他们放权，劝说他们放权，劝说他们还政于民。再积极一点，就是抗争，向专制者争权、要权。<br />
<br />
问题出来了：专制者不听呼吁怎么办？不听劝怎么？专制者不改良怎么办？专制者不放权怎么办？傻等？常跪不起而祈求专制者开恩？它不开恩又怎么办？你祈求它都抓你、杀你，怎么办？到此，诅咒革命而将改良绝对化的书生们，进入了死胡同。<br />
<br />
我这个革命派，正确的称呼应当叫两手策略派。我呼吁改良，但不拘泥于改良。我希望专制者改良，但不奢望专制者改良。如果专制者不改，就行使革命的权利。此外，别无它择。<br />
<br />
应当这么说，老百姓实际能“主动操作”的，不是改良，而是革命。改良是有权者能主动做的，革命是无权者能主动做的。革命，我们能主动操作的很多，鼓动革命舆论，发展革命组织，渗透党政军各界，发动群众示威，策动军队倒戈，等等，都是。有人最近总问我：“老王，你近来在干什么？”我可以坦率告诉大家，我在策划革命，具体地策划中国的民主革命，而且已有成效。<br />
<br />
但是，有一条要讲清楚：准备革命，不一定实行革命，可以引而不发，以此逼迫执政者改良，专制者改良了，也可放弃革命的进行，但，永远不放弃革命的权利。革命，应当是一把永远悬在专制者头上的利剑，它改，就不砍下去；不改，则毫不犹豫，必砍无疑，是专制者迫使我们不得不行使革命的权利。六，放弃革命的权利，等于放弃了一切<br />
<br />
不错，我们应当争取的公民权利、公民自由有很多很多――言论的自由、结社的自由、游行示威的权利、练功的权利、私有财产保障的权利、免于恐惧的权利……，可列出一百条，两百条。<br />
<br />
对诅咒革命、将改良绝对化的书生们来说，又一个死结的问题产生了：你争取这些权利，很对。但是，独裁者不给，怎么办？<br />
<br />
这时，只有一个权利可以行使，那就是争取权利的权利。争取权利的权利，就是革命的权利。放弃了这个权利，等于放弃了一切权利，因为，你放弃了争取权利的权利，就等于给自己脸上贴上了一个标语：当那些普通的公民权利――如言论自由的权利――争取不到的时候，只有坐以待毙。独裁者看到这个标语，还愿意放权吗？<br />
<br />
回到南斯拉夫革命的例子。米洛舍维奇不承认选举失败，假如南斯拉夫人民不发动革命推翻它，而是“和平、理性、非暴力”地等待第二轮投票；第二轮投票又被米氏做了票，还是“和平、理性、非暴力”，顶多来个“静坐示威”，他会怕你的示威？他会可怜你？恩赐你？别说米氏不会理会你的和平示威，连军中、警察中的民主派可能都不会看上软骨病患者。正是果断的革命措施――攻占议会大厦，才最终迫使米氏投降缴械。七，学运――工运与农运――兵运<br />
<br />
我在八九民运失败后，曾在香港《争鸣》杂志发表了一篇文章“学运――工运――兵运”，指出：这是推翻专制制度的革命必经之路，除非专制者主动改革和放权。现在，应当加上农运，而且，还应当强调一下农运。因为，中国农村近几年的农民暴动，规模越来越大，几千几万人的造反，已是司空见贯，这是农村矛盾激化的结果。我们民主革命派应当特别注意。<br />
<br />
这次南斯拉夫革命，又一次证明了我的这个模式的基本正确性。南斯拉夫的革命，起于学生的罢课和工人的罢工。在攻占国会的同时，我在电视画面上，看到他们使劲地在做武装警察的工作――底下策反的工作可能更多。结果，首都警察倒戈，军队宣布中立，到此，米氏彻底没戏了。<br />
<br />
军运，是必不可少的。军队起义固然是好的，但只要做好工作、使武装力量保持中立，人民革命就会成功。八，革命不是鼓吹出来的，是社会矛盾决定的<br />
<br />
一九九八年初，我闯关回国。不巧被捕后，中共官方查出了我带进国内的传单。他们惊讶地说：“你在国内散发这个东西，老百姓看了，那还了得？会煽起革命和动乱的。”我对曰：“如果几张传单就能煽动起革命，说明这个政府毫无存在的价值。在美国，有好几个共产党，成天宣传革命，推翻美国政府和资本主义制度，人们把他们看成半疯。”的确，革命不是鼓吹出来的，没有革命的客观条件，怎么鼓吹革命，也是白费。就象一捆湿木材，怎么点，也烧不起来。反之，一片干柴，一个火星就可成燎原之势。在革命条件已经形成的地方，你再跺着脚地阻止革命，也是没用，革命该来就来。为什么？革命不主要是取决于人的主观意志，而主要取决于客观条件，就象那位南斯拉夫革命者十多年前所预言的，南斯拉夫会发生解体，这是由其内在的矛盾决定的。中国现在有没有发生革命的条件？当然有，而且革命的条件逐步成熟。<br />
<br />
有人问，经济改革后，人马生活不是提高许多么，怎么还会爆发革命？让我们简单举一下俄国一九一七年二月革命和十月革命的例子。在两次革命前，俄国沙皇进行了经济改革，与中共的当今改革比，可说是成功百倍。经过改革，俄国成了世界第一大农业出口国，从工业产值讲，俄国也成了世界强国。这种表面上的经济成就和百姓的生活改善，使原来改革前鼓吹革命的知识分子纷纷转变了方向。他们甚至办了一本杂志“路标”，开宗明义地说，形势一片大好，路标应当转向，他们要“告别革命”，他们号召知识分子应当与沙皇政府合作。论点与现在一批主张“告别革命”的中国书生们尤出一辙。他们谁也没有料到，甚至连仍然坚持革命的列宁也没有估计到，二月革命竟然突然爆发，而且迫使沙皇政权在半个月内土崩瓦解。革命的直接触发非常简单：由于运输上出了点差错，彼德格勒市场一天早上缺少了面包供应。人们不满，开始鼓噪，上街闹事。事件越演越烈，沙皇派其禁卫军前往平息事件。但是，禁卫军到了彼德格勒，被群众包围，他们听了老百姓的诉说，觉得有理，不但没有镇压，而且宣布与人民站在一起。形势急转直下，半个月内，俄国其它地区纷纷背叛沙皇，尼古拉二世被迫退位。那些诅咒革命、要“告别革命”的知识分子们惊呆了，这是怎么回事？为什么改革这么成功，却突然爆发了革命？他们进行了反思，发现：是人们的“清算心理”在起作用。什么是“清算心理”？它是怎么产生的？原来，俄国当时的经改，与中共今日的经改，有很多雷同之处。特权分子利用权力大发利市，老百姓心理很不平衡。尽管老百姓生活有了某种的改善，但人们有一种“清算心理”：他们要清算特权分子，要讨回公道。“清算心理”的产生是很自然的。不用提那些大批下岗的职工，就说说普通薪水阶层吧。改革前，我挣一百元，你特权分子挣二百元，是有差别，但尚可忍受；改革后，我挣的是多了一些，一月五百元，可你特权分子每月却可挣五十万元、五百万元，而且只是一个批文、一个签字，就财源广进。老百姓的心理怎么能够平衡？就是这种“清算心理”，或称“算总帐心理”，或者说是“讨回正义和公道”的心理，终于触发了革命。二月革命后的“清算”并不彻底，人们的失望又导致了第二次“清算行动”――十月革命。<br />
<br />
仔细研究一下古今中外的历史，便可发现，革命往往不是在老百姓生活最苦的时候发生，而是在改革的带动下生活水准向上攀升、但出现分配严重不均、社会严重不公、人们的原先预期与社会现实形成落差的时候发生。所谓“不患寡，而患不均”是也。而这，正是当今中国大陆的现状。<br />
<br />
如今，中共特权横行，无官不贪，警匪一家，贫富悬殊，失业越来越多，民族矛盾激化，军队苛扣军饷，共干变成恶霸，内地与沿海形成巨大反差，公、检、法机关 ――这个原本维护社会正义的最后防线的机关竟然成了制造不公正的黑色交易所，这一切，导致了老百姓心理的极端的不平衡。社会缺乏正义，人民渴望公义与公理，一种“清算”运动，正在酝酿。一句话，中国正处在革命的前夜。九，中共错失并封杀改良的机会<br />
<br />
预防革命的最佳途径，就是成功的改良。不但经济要改，政治要改，意识形态也要改，要全方位的改。改革了，社会的压力就会减少，社会的矛盾就会消化，不会累积、激化到触发革命的地步。<br />
<br />
中共曾有过几次绝佳的政治改革机会。一是七八、七九年西单民主墙时期，一是八九学运时期。可惜，这两次机会都被中共自己杀掉了。<br />
<br />
现在，中共是否还可能进行政治改革呢？<br />
<br />
做为一个和平主义者，我真希望中共能够良心发现，走上政治改革的道路。问题是，政治上中共怎么改？所谓政治改革，就是放权于民，就是三个开放：<br />
<br />
一，开放选举，还选举权于人民；<br />
<br />
二，开放言论，还言论自由于人民，允许人们自由办报、办电台、办杂志；<br />
<br />
三，开放党禁，还结社权于人民，使反对党合法化。<br />
<br />
形势发展到今天这个地步，我敢保证，任何一个方面的开放，中共都会迅速失去政权，它会干吗？尤其应当指出，中共连南斯拉夫共产党还不如，目前它已经变成了一个地地道道的犯罪团伙。它十分恐惧，恐惧下了台后面临的清算。因此，中共已经走到即使想进行政改、都不敢政改的地步。何况，中共江泽民等最近还在信誓旦旦：绝不搞“资产阶级”议会那一套，绝不搞政治改革。中共已经把改革之途再次封死。<br />
<br />
改改不行，唯有革命。社会的前进是阻挡不住的。十，革命就是节日<br />
<br />
雨果说，革命就是节日。当南斯拉夫革命派攻占了国会大厦和电视台的时候，我看到人民在大街上载歌载舞，象过节一样。我当时心想，中国早晚也会有那么一天。<br />
<br />
习惯上，一提革命，人们就会想到血腥的共产革命。时代不同了，革命的形式也随时代发生了改变。不错，革命是自下而上发动群众、推翻专制的大规模运动，但它在现代化的条件下，却显得特别具有节日的欢快性。菲律宾是这样，印尼是这样，南斯拉夫也是这样。书生们，不要怕，革命不见得血腥，反之，跪着祈求专制者改良，反而会导致血腥的后果，六四镇压，就是明证。南斯拉夫革命死的人多，还是六四“改良”死的人多？印尼革命死的人多，还是六四“改良”死的人多？改良的损失一定会小？革命的损失一定会大？此论休矣。<br />
<br />
还有什么“以暴易暴”论，这种因果绝对论，至今还在象金箍咒一样，箍在某些书生的头上，谈“暴”必痛。古有美国的暴力革命，今有罗马尼亚的武装起义，还有菲律宾的飞机轰炸总统府，哪个导致了专制？哪个验证了“以暴易暴”？印尼的革命，南斯拉夫的革命，都有少许暴力的场面，革命人民建立的，一定就是专制？此论也可见鬼去也。<br />
<br />
还请记住：革命的手段不一定导致专制，同样，民主的手段也不一定导致民主。米洛舍维奇是怎样上台的？是选票。菲律宾的大独裁者马可士是怎样上台的？也是民主选举。更绝的是，世界上最恐怖的恶魔之一――希特勒，同样是民主选票把他送上了权力宝座。因此，迷信“一定要用民主的手段争取民主”的绝对因果论者，也要小心一点才是。十一，该出手时未出手――八九民运最惨痛的教训<br />
<br />
我至今都在惊讶：谈到八九民运为什么失败时，一帮人整天在那里揪住鸡毛蒜皮的小事辩来辩去，什么不应当绝食，什么应当早点撤退广场，什么不应当矛头对准邓小平，等等等等；辩来辩去，好象都在为镇压者解套，好象成了镇压有理、绝食有错！荒唐啊，荒唐！可怜啊，可怜！<br />
<br />
八九民运的真正教训是什么？是主流民运没有认识到“推翻政府乃天赋人权”，是主流民运没有认识到“夺权乃天赋人权”，是主流民运没有认识到“革命乃天赋人权”，是主流民运没有认识到“放弃了革命的权力就等于放弃了一切”。以至当革命的形势到来时，“该出手时未出手”，错失了良机。思想上没有推倒柏林墙，真正的柏林墙就不可能被推倒！一个六四后跑出来、在中共政府里面工作的干部对我亲口说，六四屠杀前，确有手握军权的军官――他的同学，找他商议兵变的可能，但他竟然劝阻了那两位军人。谈起此事，他至今捶胸顿足，后悔不已。<br />
<br />
孙中山说，知难行易。认知了“推翻政府乃天赋人权”的道理，才能在行动上去推翻一个暴虐的政府。没有民主革命的理论，就没有民主革命的行动。祛除革命的恐惧比发动革命要困难千倍百倍。中国的知识分子，没有治愈政治上的软骨病，就没有资格谈论中国的民主革命，更惶论领导中国的民主革命。<br />
<br />
下一次民运高潮到来时，不失时机地指导群众发动革命，该出手时就出手，攻占政府机关，攻占电台、电视台，推倒中共贪污、腐败、残暴的专制，及时宣布地方或中央临时民主政权的建立，我想，这才是对六四在天之灵的真正告慰，才是对子孙万代负责的大德表现。(二零零零年十月七日)</div>

							
							
							
							
