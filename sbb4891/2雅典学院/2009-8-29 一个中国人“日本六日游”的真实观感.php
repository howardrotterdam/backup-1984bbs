													<h2>一个中国人“日本六日游”的真实观感</h2>
						
						
																											<div id="postmessage_219227" class="t_msgfont">震撼！从日本回来不得不对日本社会竖大拇指<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 昨天刚刚从日本回来，参加了一个6天行程（2009年8月13日至17日）的旅游团。本来对日本游没有什么兴趣，主要是为了陪老婆去的，但我没有想到这次去日本对我的震撼是那么的大。我不是一个常发贴的人，但这次的心灵震撼让我不得不写出来。<br />
　　如果说只是观光，日本的旅游资源绝对比不上我们中国，我想这也是我对日本旅游没有兴趣的原因吧，而且去之前据说很多中国人到日本旅游的目的更多的是为了购物，尤其是女孩，那里的化妆品、名牌确实比国内便宜一些，而且种类齐全。我几乎没有什么准备去的，所以更多的时间留在了对日本社会方方面面的观察上，以至于6天行程对我的内心震撼如此的大。<br />
　　首先说说我看到日本的经济情况。<br />
　　日本是一个经济非常繁荣的国家，可以说这边都是集团性的经济动物，大型的成功公司比比皆是，无论是东京还是大阪，甚至一些小城市，都能看到整齐而繁华的街道和商店，那种繁华不是一两张照片能感觉到的，你如果在那样密度的繁华区域走很久之后，你会感到惊讶的。尤其是自动贩卖机和便利店，非常之多，寺庙、办公楼、停车场、甚至很偏的一些仓库区以及每一层酒店的楼道里都能很轻松地看到。很少能看到我们比较常见的那种家庭式的“小卖部”，基本上这些自动贩卖机和便利店都是连锁公司的行为。在日本的银行很少看到向中国这样排大队的现象，因为很多缴费业务在这些便利店就可以完成。在商业上，日本的零售业可以说是一个绝对的成功，就像血管一样把商品的销售渠道做到了极致！这样也从另外一方面完全阻挡了外国零售商的进入，2005年，家乐福和沃尔玛均从日本退出就映正了这一点。事实上，零售渠道的建设其实同样具有战略意义，对于控制消费品，推动社会建设有着不可替代的作用。而反思我们现在，家乐福、沃尔玛、华堂、 7-11等这些零售商正以最快的速度在我过各地扩张。也许在大城市有些东西感受不深，如果你在一个中小城市，就会发现类似家乐福、沃尔玛这样的大型超市在不断击垮着当地的商场！可以说，日本牢固地守住了自己的这个重要领域。<br />
<br />
　　另外有必要提一下的是日本的农村，从农村也可以看到日本经济的发达。首先农村的住房和城市的居民住房一样漂亮，都是那种经过设计的小别墅，或者说那种阁楼建筑，便利店、卖车店、饭店和城里基本没有什么差别，而且日本的田间居然能看到大量的路灯，种茶的地方还有那种小风车，用来吹冬天的雪花，避免冻伤茶叶。农村还能看到球场和高档餐饮场所。<br />
　　另外在东京、大阪的商业区你会发现他们的商铺密集程度非常之高，店铺虽然大多数都是中小型的，但整体占地面积通常都能走一个小时，而且人流量都很大，这从一个侧面体现出了日本人的购买力。下面这些数字是从网上找的：日本人固定月工资比中国要平均的多，差异幅度有，但是不会特别的大，一般可以简单地计算：大约多少岁每月就可以挣多少万日元。例如30岁拿30万日元，50岁拿50万日元，再加上夏季和年终的两次共2-5个月的奖金。所以30岁年收入一般是450万日元，40岁年收600万日元，以上都是税后收入。人民币对日元可以用100：7来计算，即中国月平均工资/物价=1500元/7元=214 件。日本30岁工人的月工资/物价=300000日元/100日元=3000件，日购买力中国214件/30 天=7件/天，日本3000件/30天=100件/天。反过来理解物价/工资比就是中国物价一天工资能买7件，日本一天工资可以买100件。这样的对比太能说明差距了。<br />
<br />
　　再说说日本的环境<br />
　　两个字可以概括：干净。这个很大程度上建立在日本的国民素质基础之上。<br />
　　空气质量：基本上我到的这几天都是那种清澈的蓝天，一路上我都在找那种冒黑烟的工业区，很遗憾，我虽然路过了很多工业区，但确实没有发现那种冒黑烟的烟囱。也看到了一些烟囱，和我们的不同，我们的烟囱是那种圆柱型，人从外面可以往上爬的那种，而我看到的烟囱都是方柱型，带窗户，一看就是人可以在里面走楼梯上去的那种，而烟囱的顶端有点象一些汽车的排气管形状，基本没有看到冒气的。我猜想烟囱里面的结构应该是有很多功能性的。另外日本满大街都是小型两厢车，也为减排作出了贡献。当然，海洋性气候可能也帮了他们很多忙。<br />
<br />
　　农村和城市：日本的农村环境保护的非常好，基本没有那种尘土飞扬的地方，要么种田，要么住人，要么就是那种一看都有很久了的密林。那种自然的树林一看就没有经过砍伐过，都是植被非常密集的那种，显然不是人工种植的，据说日本人不砍自己国家的树林，所以你会发现那种长满青苔的大树和密集的野草地特别多。比如我们去富士山的时候，路上全是茂密的树林，里面能看见有很多自然倒下的树木，上面长满了青苔和各种植被，显然已经倒了很多年了，但没有人去搬走，仍然让它保持着最自然的形态。说实话，那样的原始森林，人进入根本无法下脚，所以也没有人跑到里面破坏。日本城市中也经常能看到很多一看就是很老的树和小树林，另外还有很多整齐的草地，是可以践踏的（想到日坛公园，有一对老外在草地上铺东西坐下睡觉，被保安轰走时的表情了，他该多么疑惑呀）。但所有草地的边际都是非常整齐的，也没有我们常见的那种草地上被人踩出来的“人造小路”。由于灰尘少，草地和树木都显的极其干净。<br />
<br />
　　居民住所和汽车：在日本无论是农村还是城市，最常见的民宅是那种日式，或是有些欧式的小阁楼，有点别墅的意思。一般阁楼前面还会有自家种的竹子、树或者花什么的，据说自家前面绿化是否整洁直接体现除这家人的品味和勤奋程度，一般都是由家庭主妇负责的。这种日式的民宅基本结构都是木头的，我看见一个正在盖的房子，整个框架和屋顶全是木头。这样的民宅有一个好处就是可以自由设计，所以很多房子都设计的很漂亮，可以是欧式的，也有日式的，或者土洋结合的。<br />
<br />
　　日本很多家庭都有两辆汽车，但大多数汽车都是两厢型的小车。不得不说的还是干净的问题，路面上你绝对看不到沾上泥土，或者旧的掉漆的汽车，每辆车都跟新车一样，包括大客车和卡车，基本上所有车的轮胎和轮毂都是新的一样，这点我觉得比较惊讶，很多大车的轮毂上的螺丝都是闪亮的。环境好是一方面，但下雨什么的难道不会锈的吗？我的车是换了不锈钢的轮毂，但日本车难道都是不锈钢的轮毂吗？<br />
<br />
　　吸烟问题：在日本边走边抽烟是禁止的，虽然也没有人管，但是确实没有人边走路边抽烟，哪怕是街头混混。街道一般都有专门的吸烟点，那种上面带网格的垃圾筒，里面会有水。在东京还看见那种烟草商提供的免费吸烟点，可以坐在空调房里抽烟，而且还提供一些小服务，比如喝水、充电什么的。另外，街头还有一种象小门脸一样的吸烟点，上面写着“20岁以上进入”，“有权查看身份证”，旁边就是提供这个小门脸的烟草商的店，我以为那个查身份证不会有人那么认真，更何况查的人是卖烟的，多卖点才好呢。谁想我真的碰到一个人被烟草店的人查，面向很小的痞子样，不过看完身份证，确实满了20岁。<br />
　　开始我觉得挺奇怪，干脆不允许在公共场合抽烟不就行了，为什么仅仅不能走路吸烟？后来在一个吸烟点看到了带图示的解释有点啼笑皆非，理由有叁：1、吸烟时大人手的位置和小孩头的位置相当，容易碰到小孩（这点勉强算个理由）2、烟蒂容易堵塞下水道；3、香烟烟蒂容易烫到小猫小狗和蚂蚁等动物（这点太强了！）<br />
<br />
　　日本女人抽烟的很多，很多地方能看到家庭主妇带着孩子在吸烟点抽烟的。<br />
　　另外，日本的饭馆只要没有贴着禁烟的标示，就可以抽烟，习惯了在北京的饭馆不能抽烟后，到日本还有点不习惯，日本人不忌讳在饭馆抽烟影响其它人，但烟灰缸你要主动要，桌子上不会专门摆着一个烟灰缸。<br />
<br />
　　卫生间：日本的公共卫生间都是免费的，无论地铁里的还是街道上的，或者农村的，干净程度都能媲美首都机场的卫生间，都是带冲洗烘干装置的那种马桶，女士的还有“音姬”，就是坐在上面会发出流水声，掩盖不雅声的装置。每个卫生间都有放卫生纸的装置，但不是所有卫生间都有卫生纸的，比如靖国神社里的卫生间就没有。<br />
<br />
　　垃圾：日本已经完全实现了垃圾分类，这点挺可怕，这样他们的垃圾有效利用率会提高很多，除了节省资源外，还额外创造财富。垃圾分类分为纸类垃圾、塑料瓶、易拉罐和其他，专门看了很多垃圾桶，确实没有乱扔的。他们的意识怎么能如此的统一？<br />
<br />
　　水：去之前听说日本的自来水都是可以喝的，但到了以后才发现不是这样的，有些是的，但也有不能喝的，很多街头、公园、地铁等公共场所都会提供那种可以喝水的龙头。大多数酒店的自来水也是可以喝的，但最好看到相关标示再喝。除了自来水外值得一提的是他们的自然水资源。在飞机快降落的时候（大阪机场是在海上填出来的），看到大面积的海岸，没有那种白色泡沫，水都非常清澈，你会发现日本的河流也是如此，就连居民区的小溪流都是清澈见底的那种，很难见到漂流物，更没有恶臭。我在南方呆过很多年，看似简单的一个现象，但背后制度、科技、投入、执行力和自觉力的支撑很强大呀！<br />
<br />
　　再谈谈日本的交通。<br />
　　日本出行包括几种手段：自驾车、地铁、出租车和自行车，摩托车也有，但绝没有那么密集，都是男的骑的那种比较彪悍的摩托。<br />
　　先说说这次去日本坐的比较多的地铁，他们也叫“电车”。这可以说是日本最常用的交通工具了。如果你看到东京的地铁图的话，你会晕的，跟蜘蛛网一样。大多树地铁的地面口都很小，有的就像一个卖烟的小铺那么大点，但如果下到地下的话，你会发现别有洞天。日本的地铁由于建设的很早，现在看来都比较旧，但都很干净，而且有女士专用车厢（但好像不起作用，男的一样大摇大摆的进）。买地铁票是选择钱数不是选择去哪站，一般地铁票从160到500（人民币11元到 35 元）按远近不等。日本人在地铁里面看书和玩手机的居多，很少有说话的，所以地铁里非常沉默，而且给老人让座不是普遍习惯。和我们不一样的是他们上下车都非常谦让，没有人抢座位。<br />
　　日本的公路和高速路都比较窄，大多数高速都是单向2车道的。日本车也非常多，但都特别的规矩和默契。他们可以闯黄灯，但是决不闯红灯，尤其是人行道最能体现车与人的默契，无论是行人还是司机，绝对遵守红绿灯指示（一个只有三四步远的人行道，明显两边都没有车，可是没有人过，因为是红灯），没有红绿灯的人行道，车辆会主动让人。这样做的好处是大街上的车开的都非常快，在日本，行人没有走人行道被撞，司机不负责。路上很难见到抢道和加塞的车，车和车之间都异常谦让，当并道时，都遵循一走一让的原则。在高速路上，我们遇到了堵车，我们的大车始终和前车保持着三四个车位的距离，旁边一条道的车虽然也堵在路上，但是没有车到我们前面加塞。大家显的异常友好。让我想起在国内有个周末去十渡的路上，一条双向两车道的路愣被抢成了单行四车道，连两边的应急车道都挤满了加塞的车，结果两边谁也别走，40多分钟的路，我开了5个小时！而且每天上班的路上我都恨不得给自己的车上装上铠甲，去冲撞那些自私的加塞车。<br />
<br />
　　日本的出租车很有特点，司机都是老人，那种头发都花白的，还有留长胡须的。车基本上都是老式的丰田皇冠那种，但很新很干净。这些司机都非常地有礼貌，穿着虽然不统一，但都是西装革履的，还有人戴领结的。我在路边观察过一个老司机，他会下车站到乘客上车的那边戴好帽子，然后象站岗一样地站在那里等客人，很标准的站姿。没有想到的是他居然姿势不变地站了有20分钟才来了一个客人，他主动地打开车门，用手互着客人的头，等客人坐进去之后跑到车那一边去开车。那天可是大太阳的中午呀，这太让我震撼了。当然不是所有的出租司机都会站在外面等，但基本上你会发现他们的出租司机都是礼仪专家。<br />
<br />
　　日本的自行车也很普遍，而且大多数自行车都带灯，且车很新，很难看到旧车。<br />
<br />
　　接下来说说我感受的日本文化。<br />
　　传统文化：个人认为日本文化虽然山寨了我们中国文化，但他们保留和继承的非常好，并且很纯粹。日本的文化继承在今天看来是值得夸耀的。街头巷尾你都能看见穿着传统和服的日本人，包括很多商家的营业人员，对他们而言是很普通的事情。不光服装，包括很多的建筑、用品、食品等等，你都能找到日本古代的感觉。在这些表象下面，也许还有他们的精神。日本每年很多自杀的人，去年达到了5万人，很多人自杀是因为责任心，比如在公司犯错，或者失误等等，这样自杀的人在整体中不在少数。这个现象虽然极端，但是更普遍的是他们整体内心中的那种可怕的责任感和自尊心。我是作企业高管的，事实上现代企业中最主要的特征是对人的管理，把人管好，让每一个人都发挥出最大效率，企业基本上成功了一半。而日本人的这种民族潜意识是把每一个人都变成了负责任的工具，这样的企业效率和竞争力会是惊人的！另一方面，这种良好的传统继承也更加加深了他们的凝聚力。只可惜，他们忘了他们的祖先是中国呀！！<br />
<br />
　　除了传统文化外，日本的现代文化也十分的发达。什么漫画、电视剧、音乐这些我就不说了。我说说我看到的一些现象吧：在日本，你会发现所有的店面、商品等等人造物都特别的精致，除了制作精细外，还有一个就是他们都经过精心的设计。就拿各种小点心来说，每种点心别管好不好吃，光包装你就会觉得想要一个，非常精美。还有就是他们的商业店面，都设计的非常有特点，很难见到有那种喷绘布一贴就是个店面的。这个现象从一个侧面说明日本人普遍审美水平，社会已经不容那种没有审美的东西了。此外，在大城市里看见书店我觉得不新鲜，但在农村看到一个装修精美的大书店就比较奇怪了吧，我想精明的日本人是不会在一个没人看书的地方开书店的！书店里面绝不只是漫画和杂志呀，那些是少数！<br />
　　当然日本的色情文化也很发达，成人杂志一般便利店和书店都能买到，还有DVD什么的，还有很多色情器械、服装、手办等等，这些也是日本文化的一部分。<br />
<br />
　　说说我接触到的日本人吧。<br />
　　总体而言日本人面上都非常客气，很多帖子都提到在日本问路。我在日本也向很多日本人问路求助，那种客气让你觉得很过分。在地铁里一个年轻人为了给我们指路，专门下车，带我们到另一个站口，再跑回去。警察也是那种微笑服务。听到日本话想踢他们的感觉顿然消失。日本的服务员都很有礼貌，如果你买东西的话，一定不要自己拿着，他们会帮你拿着，包装好，再付钱。但日本人的英语水平普遍不高，会说的我觉得也挺难听懂的。比较讨厌的是他们明知道你是外国人还叽里呱啦的日语比划，哪怕是飞机里的空姐。<br />
　　另外，日本人好像确实对政治不感兴趣，在涩谷时，突然看到一个竞选党派（居然写着“幸福实现党”）白色的宣传车放着大喇叭，里面的人好像是我在他们到处都贴着的竞选海报上贴着的一个女的，拼命地向大街上的人招手示意，不过好像没有什么人理他，连驻足观看的人都很少。<br />
<br />
　　再说说臭名昭著的靖国神社吧。<br />
　　我去的时候是8月16日，他们所谓的“大祭”是8月15日，那天我看到很多的地方膏药旗降半旗。我是中午10点多到的，人并不多，稀稀拉拉的，门口有卖日本古董的小市场，很多二战期间的东西，看到一本海军舰船当年的日志并配有图片，还是比较动心的，合人民币300多块，但看到摊主穿二战军服的鸟样，没有了心情。还有当年8月15日日本投降那天的报纸。靖国神社总体不大，也就是个小公园那么大点，随意进出，但参拜的地方不能拍照，我照了几张，马上有警察过来阻止。后来我才知道不是靖国神社这样，日本所有的神社、庙宇参拜的地方都是不能照相的。这种神社基本全是用木头造的，估计很容易点燃。神社门前那条路上有很多的树，树上面都会有一个牌子，写着一些二战日本战犯部队的番号，好像他们每个战犯部队后来都成立了纪念会之类的组织。靖国神社里面还有三个给犯了罪的战争动物立的碑，信鸽碑、军犬碑和战马碑，这么看来，靖国神社基本是一个纪念畜牲的地方。在里面转了一圈，没有看到狰狞的右翼分子，只有一个可能是二战老兵的老头在休息区里孤独地吹着口琴，吹了很久，没有人理他，然后一拐一拐地走了，祝他早点收拾收拾去世吧。<br />
<br />
　　最后我需要表明一下我此行最终的立场。<br />
　　去日本之前，我对这个国家非常地排斥，而且充满着警惕。但此行让我的内心很难平静，作为一个敌国，一个不肯承认罪行的罪恶国家，我们对他的了解太少了。从各个方面看，日本超出我们太多了，这样的事实是无法回避的。我们太需要去了解这个国家，并学习他的东西了。下飞机后，在首都机场，国航的服务厅里，这边接待着客人，那边肆无忌惮地几个服务人员大声说着明天早上凑钱卖煎饼的事情。打车，一上车刚说出自己要去的地方，司机就面目狰狞地嚷嚷“到20 公里了吗？”，既不情愿地开着车，打着手机（他们不到20公里的话，可以回来不用排长队拉活），一路上不断有人抢道，然后他也抢，在自行车道上不断杀出向前蹭的车。看到这些，回到祖国的美好心情顿时跟吃了个苍蝇似的不是滋味。国民素质的差距让我很难骄傲起来。在敌国，我不自觉地成为了一个有礼貌，谦和的人，因为他们的国民对所有人都是那样的，可在我们自己的家园里，我们的国民已经忘了自己的根，都在尽力表露着自己的自私和彪悍，我用不了多久就要去习惯和同胞斗智斗勇。可悲呀！<br />
<br />
　　昨天晚上看电视，无意间看到一个国学大师的培训课，不禁感悟，我们失去的太多了。要知道，日本的礼仪和文明是从中国传过去的，而现在的我们，别说内在的东西了，就连基本的形式也消失殆尽了！“知己知彼，百战不殆；不知彼而知己，一胜一负；不知彼，不知己，每战必殆”，我突然发现，我们居然在不知不觉中进入到了“不知彼，不知己”的状态当中。我们到底是中国人还是“糟蹋中国的人呀”？<br />
<br />
　　写这么长一个东西的最终目的不是抱怨，也不是艳羡，而是希望借助日本一行的感受和所见所闻给我深爱的祖国的同胞们一个警醒！与敌国相比，我们缺的不是物质，而是心！我们需要集体性地“补心”了呀！<br />
<br />
　　我们古老的祖先们用自己的智慧创造了具有“大乘气象”的中国文化――国学，这是我们老祖宗留下的最宝贵的东西，从齐家到治国，从礼仪到兵法，这些宝贵的东西我们应该捡起来了，是到了国民精神复兴的时候了！现在的战争是隐形的战争，是经济之间的战争，用得不是彪悍和蛮力，用的是高素质的人的智慧及知识，这样的人组成的经济体才是最锐利的现代武器。强烈建议大家有机会一定好好学学老祖宗的东西，让自己让身边每一个人都进步起来。每天自省一下，我的工作做好了吗？我今天违法或违规了吗（乱闯红灯就是违法，乘扶梯不站右边就是违规）？我伤害别人了吗？我学习了吗？……只有我们每一个人自强不息，中华民族才能生生不息！</div>

							
							
							
							
