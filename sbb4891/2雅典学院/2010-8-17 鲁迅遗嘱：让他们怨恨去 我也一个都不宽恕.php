																											<div id="postmessage_789271" class="t_msgfont">国家和人民是两回事。<br />
比方说：台湾和大陆的官方地图上，两地都属于中国。台湾独立，中国貌似就要受损，但实际上，于人民真正有损害吗？<br />
台湾事实上就是独立的，真要是独立了，双方互相承认，双方反而可以少花很多军费。<br />
同理，香港、澳门，对中央财政一点贡献也没有（反而多少要花些钱，比如驻军什么的），就算中央财政是为大陆人民服务的，香港澳门独立，对人民又有何损？<br />
