													<h2>鲁迅遗嘱：让他们怨恨去 我也一个都不宽恕</h2>
						
						
																											<div id="postmessage_786887" class="t_msgfont">学者陈漱渝主编的《一个都不宽恕》，1986年曾由中国文联出版公司初版，当时该书跻身畅销书排行榜前列。日前，《一个都不宽恕》一书又在人民日报出版社再版。 <br />
<br />
　　接受记者电话采访时，陈漱渝表示，论争的内容涵盖了20世纪早期鲁迅和复古派、现代评论派的论争，30年代左联内部的几次论争等等，论争人物涉及吴宓、章士钊、梁实秋、田汉、郭沫若、顾颉刚等，“而论战起因，既有不同文化营垒的分歧，又有由误会引发的私人纠葛等。” <br />
<br />
　　翻看书页，我们依旧能闻到80多年前文坛浓郁的硝烟味，可以看到以鲁迅为旗帜的文化新军和明明暗暗论敌进行较量的波澜壮阔的历史画面。 <br />
<br />
　　　　不宽恕那些本性不改的敌对势力 <br />
<br />
　　海南周刊：一个都不宽恕的书名是怎么得来的？ <br />
<br />
　　陈漱渝：书名取自鲁迅杂文《死》，写于鲁迅去世前44天。文中披露了鲁迅重病时拟定的七条遗嘱。 <br />
<br />
　　其中第七条是：“损着别人的牙眼，却反对报复，主张宽容的人，万勿和他接近。”鲁迅补充道，“欧洲人临死时，往往有一种仪式，是请别人宽恕，自己也宽恕了别人。我的怨敌可谓多矣，倘有新式的人问起我来，怎么回答呢？我想了一想，决定的是：让他们怨恨去，我也一个都不宽恕。” <br />
<br />
　　海南周刊：我们该怎么理解所谓“不宽恕”？ <br />
<br />
　　陈漱渝：鲁迅通过对中国历史的深入研究和对中国现实的深刻洞察，总结出了一条重要的经验教训：“犯而不校”是恕道，“以眼还眼以牙还牙”是直道。中国最多的却是枉道：不打落水狗，反被狗咬了。这其实是老实人自讨苦吃。 <br />
<br />
　　鲁迅还举了一个例子：辛亥革命之后，绍兴都督王金发宽恕了杀害秋瑾女士的谋主章介眉，待二次革命失败，章介眉勾结袁世凯反把王金发枪懒恕Ｕ饩徒小白约揖蚩幼约衣瘛薄 <br />
<br />
　　海南周刊：鲁迅“一个都不宽恕”的对象是谁？ <br />
<br />
　　陈漱渝：鲁迅提倡“一个都不宽恕”，主要是针对压迫者在暂时失势时提倡的“恕道”，主要对象是那种本性不改的敌对势力。 <br />
<br />
　　在实际生活中鲁迅宽容了不少曾经伤害过他的青年，但他不宽容那种一方面贻害于人，一方面又骗以“宽恕”美名的伪善者。因为对这类人实行“恕道”，只能让他们赢得喘息时机，卷土重来。鲁迅的主张是“打落水狗”。 <br />
<br />
　　　　中国新文学开道的劈山巨斧 <br />
<br />
　　海南周刊：能谈谈论争主要集中在哪些方面吗？ <br />
<br />
　　陈漱渝：鲁迅参与的论争集中在三个方面：一是新文学阵营跟守旧派的论争，二是新文化运动中激进主义跟自由主义的论争，三是左翼文艺阵营内部的论争。这些大多是思想文化领域的论争。只有跟民族主义文学的论争政治上属敌对性质。 <br />
<br />
　　海南周刊：论争的起因有哪些？ <br />
<br />
　　陈漱渝：论争起因无法一言以蔽之。激流在奔腾时，如果遇到嶙峋巨石的阻遏，一定会卷起“千堆雪”般的浪花。中国新文学发展的征程也是这样。恰如鲁迅所说：“苟有阻碍这前途者，无论是古是今，是人是鬼……全都踏倒他。”鲁迅的论争文字就是为中国新文学开道的劈山巨斧。 <br />
<br />
　　海南周刊：我们应该如何看待鲁迅在文艺战线经历的论争呢？ <br />
<br />
　　陈漱渝：首先，我们应该尽可能掌握论争的背景和全貌。 <br />
<br />
　　历史上的论争具有其不可改变的客观性，亦即“本相”，只有贴近“本相”之后才能见仁见智，进行评价。 <br />
<br />
　　其次，在我看来，鲁迅的目标跟中国新民主主义革命时期的斗争目标相一致，因而在总体上应该肯定，并予以高度评价。 <br />
<br />
　　当然，我们不能在细枝末节、只言片语上都以鲁迅的是非为是非，对于鲁迅批评过的人物也应全面评价其历史功过。比如鲁迅批评过梁实秋，梁实秋在散文创作和翻译上自有其贡献。鲁迅憎恶章士钊，但章士钊仍然功过并存。 <br />
<br />
　　　　论争为学术文艺注入活力 <br />
<br />
　　海南周刊：了解鲁迅参与的论争有何现实意义？ <br />
<br />
　　陈漱渝：俗话说：“以史鉴今”。论争是人类古已有之的传统，能展示人类自身的智慧和风采。在论争过程中，双方的学术视野、知识水平和论辩技巧进行着激烈的抗衡，从而使文化、学术得到进一步的提升、传播和推广。 <br />
<br />
　　在古希腊，学者之间的论争蔚然成风，出现过著名的雄辩家伊萨乌斯和著名的诡辩家莱什阿斯。他们能说会道，口若悬河。 <br />
<br />
　　在中国，春秋战国时代也一度呈现过“百家争鸣”的局面。孟子对他的弟子公都子说过一句有名的话：“予岂好辩哉？予不得已也。”孟子当时论争的主要对象是杨朱和墨翟―――他认为这两位思想家的言论属于“邪说淫辞”。 <br />
<br />
　　当前中国社会处于一个重大转型时期，作家和批评家都置身于一种多元化的语境之中，特别是文学观念不断更新。因此论争是无法避免的。鲁迅在论争中的立场、观点和方法对于当今文坛的探索者都会具有重大启示意义。不过，鲁迅当年是生活在革命年代，动乱岁月，而今“和谐”二字已经成为了中国乃至全球的主旋律。进行论争的环境和对象改变之后，论争的方式和文风当然也应相应进行调整。 <br />
<br />
　　海南周刊：为什么说论争会为学术文艺发展注入活力？ <br />
<br />
　　陈漱渝：随着现代科学迅猛发展，信息畅达，知识爆炸，事物的相对性、不确定性不断被揭示，从而动摇了原来僵化的思维模式。 <br />
<br />
　　任何人都不可能穷尽真理。因此，论争双方的观点往往具有互补性，可以通过论辩发展真理。鲁迅当年参与的论争也是这样。比如“革命文学”的倡导者有些主张不符合中国国情，但鲁迅认为这一口号的提出毕竟功不可没。在“两个口号”论争过程中，双方的观点各有偏颇，因此鲁迅才认为“两个口号”可以并存。真理愈辩愈明，这就是论争提供的活力和动力。 <br />
<br />
　　　　势所必至，理有固然 <br />
<br />
　　海南周刊：在鲁迅生活的时代，文艺战线论争频发，是否也有其必然性？ <br />
<br />
　　陈漱渝：在漫长的封建社会，中国文学一直处于与世隔绝的单元的文化背景之下，呈现出一幅老态龙钟、我行我素的固执形象。 <br />
<br />
　　直到五四时期，随着外来文化的八面来风，中国文化才真正打破了文化背景的单元性质。多元并存的外来影响，使中国文学在20世纪二三十年代出现了群芳竞艳的喜人景象。在这样的文化背景之下，文学论争的产生就成了“势所必至，理有固然”的事情。 <br />
<br />
　　比如同样留学美国，胡适等新文化运动先驱者所开采的思想资源是欧美启蒙运动和科学主义思潮，手段是理智，目标是个性解放和思想文化上的反叛传统。 <br />
<br />
　　而梅光迪等“学衡派”骨干则师承新人文主义思想家白璧德，敌视科学理性主义和艺术中的浪漫主义，片面维护古典主义传统，因而走到了新文化运动的对立面。 <br />
<br />
　　又比如同样留学日本，鲁迅接受了明治时代盛行的写实主义、功利主义影响，具有强烈的政治意识和社会责任感。而创造社的批评家成仿吾却接受了大正时代尊个性、赞创造，在成仿吾看来，鲁迅的现实主义作品如《孔乙己》、《药》等，都是“浅薄”、“庸俗”之作，惟独“描写性的发动和创造，以至衰亡”的《不周山》 “最出色”。 <br />
<br />
　　海南周刊：最后，能请您谈谈编辑出版这本论争集的主要目的吗？ <br />
<br />
　　陈漱渝：这本书具有完备的史料性，收录的文章大多具有文献价值；同时又具有可读性，书中的一些导言和附录本身就是文化随笔，适合不同层面的读者阅读。 <br />
<br />
　　不了解鲁迅，就不了解一部色彩纷呈的中国现代文学史；不了解鲁迅经历的论争，也就不了解鲁迅。 <br />
<br />
　　鲁迅认为，“战斗的文章”，是他老师章太炎一生中最伟大的业绩，对鲁迅亦应如是观。本书除了提供了详实的原始资料之外，每一部分都有编者新近撰写的前言导读，提供了鲁迅研究的最新成果―――重版这本书进行的最大调整就是几乎更新了全部导读。</div>

							
							
							
							
