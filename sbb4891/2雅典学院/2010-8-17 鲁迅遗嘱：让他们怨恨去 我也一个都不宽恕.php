但是，这也不是说“都独立了就好”。都独立了就好，美国还打南北战争干什么？有时候，人民的利益也需要一个中央政府来维持。象新疆，各民族自古混居，维吾尔人妄图建立一个政教合一的独立国家，妄图象印尼、马来西亚那样屠杀、歧视华人。这样的地方，就绝对不能让它独立，就算自治，也只能是地方自治，不能民族自治。不能让民族身份成为他们反对我们的人民的理由。<br />
对于这一层关系，很多人是不明白的，他们总以为，只要反对国家，就是维护人民，这种思想，其实就是共产主义的一部分。<br />
至于某些左粪说：我们不能允许XX独立，开此先例！这种话的反驳很容易：第一个独立的地方是外蒙，你小子不反俄，反而反美，就是汉奸。</div>

							
							
							
							
