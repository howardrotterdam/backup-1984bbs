<br />
2、“官僚集团的民主性”<br />
<br />
　　不过，人们免不了奇怪：专制政权的特点是上级决定下级，虽然官僚追求自身利益或部门利益，但是高端当权者对下属部门不顾全局的行为，为何会听之任之，不进行干预和统筹领导呢？<br />
<br />
　　这里不设想专制者昏聩的情况，虽然在专制历史上，被虚假信息包围的专制者落入昏聩远高于其保持明智的可能性。然而更能反映中国权力体系本质的，却是专制者即使明智，也无法扭转下属部门共谋形成的局面。这是因为，在专制体制中，专制者的决策只有通过官僚体系进行具体操作才能贯彻。正是这一点决定了，专制权力对外虽可以绝对专制，对内却存在专制失效的可能。只有在不损害官僚集团利益的前提下，专制者才有对官僚集团一言九鼎、说一不二的专制性；而只要损害官僚集团利益，官僚群体就会共谋合作，利用拖延、扭曲、不了了之等手段，在具体操作过程中把专制者的决策架空，使之无法推动和落实。这时，专制权力的专制性就大打折扣，我把这种状况称为“官僚集团的民主性”。<br />
<br />
　　无论是在古代王朝还是近代的专制政体，“官僚集团的民主性”都广泛存在，它不会体现于制度和程序，只是以官场潜规则的方式发挥作用，是一种物竞天择演化形成的机制。官僚之间在具体问题上有竞争，总体却是相互联合的共同体。他们对自身利益的得失极其敏感，精于谋利，容易形成共识；而且无需串联和组织，利用现成的官僚体系就能默契地共谋，达成相互庇护，成本低，风险小。因此，专制者要想让自己权力得到贯彻，便需要顺应这种“官僚集团的民主性”，满足（或至少是不触犯）官僚集团的利益，才能使官僚集团成为得心应手的工具，而不是阳奉阴违的对抗者。<br />
<br />
　　当然，历史上也出现过力图对官僚集团说一不二的专制者。那除了需要具有特殊权威，还必须使用极端手段――如启用野心勃勃的新人充当酷吏，对官僚集团进行清洗，以肉体消灭的恐怖进行慑服。然而那种手段只是见效一时，飞扬跋扈的酷吏几乎都无好下场。暴君终归要死，或是清醒过来就会明白：以利益收买而不是恐怖威慑更能换取官僚集团的效力。对于多数只求利益和权力的专制者，达成与官僚集团的分肥，自己清闲地坐车，让官僚们奋力地自觉拉车，岂不比与官僚集团南辕北辙地较劲要省心和实惠得多吗？<br />
<br />
　　即使以毛泽东对中共官僚体系的权威，在他要开展触及官僚集团的运动时，甚至无法在他居住的北京发表一篇文章，可见官僚集团的能量有多大。毛最终采取的方式是越过官僚集团，直接号召底层民众造反，摧垮阻碍他意志实现的官僚集团。那种被命名为“文化大革命”的方式是史无前例和登峰造极的，最终却归于失败，原因就在于毛也离不开官僚集团。文化大革命可以摧毁旧的官僚集团，但仍然需要建立新的官僚集团来为他贯彻权力。而不管什么官僚集团，最终都会演化成利益集团，“官僚集团的民主性”也就会起作用。毛泽东最终只能无奈地退到以“七八年再来一次”的恫吓，把他的文化大革命降低到酷吏来使用。<br />
<br />
　　在文革中吃了大亏的官僚集团就此留下刻骨铭心的记忆，他们决心不允许再出现专制领袖可以摧毁官僚集团的情况。毛后几十年中共推行“党的建设”、“发扬党内民主”等，说穿了就是在加强“官僚集团的民主性”，保护官僚集团不再受领袖伤害。而中国改革的主要内容――权力下放，本质也是让官僚集团分掌更多权力。至今，中共内部已经形成了完整的制约机制，官僚集团也有了相当充分的能力；既不允许再出现酷吏式的党内清洗，也不允许再有文化大革命那类群众运动，甚至不再允许党内出现可能导致分裂的路线斗争。今天，中共党内的高层权斗比历史任何时期都弱，权力交接也有了一定程序化，其深层原因，就是这种“官僚集团的民主性”在起作用。<br />
<br />
　　这是一种本质性的转变。在意识形态至上的时代以诉诸路线斗争进行的高层争权中，官僚们只能处于被动状态，选择跟随不同路线的代表人物，然后随代表人物的沉浮而沉浮。而当维护执政和获得利益成为中共的主要目标时，执政的具体操作者和利益的主要承载者――官僚集团就变成了党的主体。失去了意识形态背景的党内斗争变得名不正言不顺，从而不再有合法舞台。随着“官僚集团的民主性”进一步加强，不利于集团整体的党内斗争将在相当程度上被联合起来的官僚所遏制。这时的中共将变得比以往更少分裂，更为务实。高端当权者不再是以往那种凌驾于官僚集团的独裁者，而是变成了官僚集团利益的协调人与代言人。这时的高端当权者只有通过为官僚集团争取利益最大化，才能交换官僚集团的支持，坐稳自己位置。<br />
<br />
　　官僚集团熟悉政权机器，善于经营，一旦建立了制约高端当权者的机制，便一定会将其尽运用到极致，他们可以在无形中决定高端权位的起伏、人事更替、政策导向等。而只要他们具有了这种能力，就不会仅限于避免发生党内清洗或文革，而是扩展到避免对自身的任何不利，以及尽可能地用于为自身谋取更多利益。因此，把所谓的“党内民主”看做是中国民主的步骤，完全是看走了眼。“党内民主”只是古已有之的“官僚集团民主性”的别称，同中国民主与人民民主毫无关系。</div>

							
							
							
							
