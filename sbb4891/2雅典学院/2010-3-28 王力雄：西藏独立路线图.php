4、中西民间的交恶<br />
<br />
　　中国民间与西方民间过去没有多少矛盾，中国人对西方人好感居多，对西方媒体也相对信任，即使在民族主义情绪高涨时，也只是针对西方政府。而西方人对中国人也无恶感，虽然西方社会与媒体经常批评中国政府，却是把中国民众当作专制政权的受害者。然而在这次西藏事件中，双方民间却直接交锋并且对立起来。中国民众对西方媒体大加讨伐，对西方民众也恶言相向。这种态度转变，是中国官僚集团舆论战的成功。不过舆论战所依赖的条件――官方垄断信息，对于成功煽动中国人是必要条件，同时却是必然受到国际社会质疑的原因。<br />
<br />
　　为了封锁信息，当局于3.14事件发生当天就在拉萨限制外国人行动自由，随之将所有外国人驱离西藏。此后各地藏区长期不许外国人进入，到处设卡检查。图像被视为最敏感资料，执行命令的军警大量侵犯人权，除了防止境外人得到图像，有的藏人仅因为用手机拍摄了照片就遭酷刑和长期关押；即使是汉人被发现在藏区拍摄了“敏感”图像也会受审问、没收设备或删除图像。严密的信息封锁使得西方媒体几乎拿不到第一手材料，只能利用间接消息进行报道。而间接消息容易出错，授人以柄，使得西方媒体在中国人心目中被普遍抹黑。中共宣传部门与西方媒体的多年斗争中第一次占据这种上风，十分得意。<br />
<br />
　　然而这并不能让西方媒体服气。中国民众一面倒的谩骂、恫吓，加上中国政府对西方媒体的制裁与施压，只能把被称作“第四权力”的西方媒体整体地推到与中国长远为敌的位置。虽然中国人的敌意会使西方媒体报道时更加仔细、更多考证和平衡，然而也会使他们对中国的厌恶增加，不仅厌恶专制政权，也厌恶中国人表现出的狂热和暴戾。可以相信，今后只要有机会，类似这次西方媒体对中国的联合围剿还会发生。而西方民众的态度很大程度上被媒体主导。得罪了西方媒体，将其推到对立面的结果，最终势必转化成西方民众眼中的中国更加妖魔化。<br />
<br />
　　事实上，正因为中国的新闻封锁，西方公众即使无法从本国媒体得到第一手消息，也决不相信中国媒体，并且质疑中国官方在西藏问题上的所有言词。因为这对他们是简单常识――只有谎言才需要封锁信息。即使封锁能做到让人不知道谎言的具体内容，但是彻底的方法就是对封锁信息者所说的一切全部当作谎言。之所以西方民众对北京奥运火炬进行激烈抵制，也是因为缺乏其他渠道进行有效表达，借此发泄对中国当局封锁真相的愤怒。<br />
<br />
　　中国官僚集团并不在意西方民间的看法，他们需要中国民众与西方社会的对立来证明自己得到民意支持，此后又利用奥运火炬在西方的遭遇进一步刺激中国民众对西方的敌意。群众运动和运动群众是极权主义的擅长。在面对宏大问题时，民众没有足够信息与知识进行独立思考，容易被煽动与操纵。中国民众虽然在多数问题上对政府并不认同，却接受了把国家统一当作基本价值观。对远离自身日常生活的西藏，多数中国人只能以简单的“分裂”与否进行判断。当官方媒体以一个声音指责西方敌视中国、偏袒“藏独”时，激发起中国人的敌意不难做到。此次西藏事件除了把汉藏关系变成种族对立，同时由把中国民间与西方民间变成了两个对立阵营。<br />
<br />
　　的确，中国民众这次对政府的支持前所未有。在互联网上，或是在国外街头，中国爱国者和西方人短兵相接。然而中国人不被允许看CNN，却狂热地反CNN；在自己国家没有示威自由，却在其他国家聚集起来再现文革场面（这些行动不乏中国官方支持和大使馆幕后组织）。一方面会让西方人更强烈地在价值观上否定中国，另一方面也会刺激西方民众把中国视为具有强烈敌意、终有一天会威胁自己的整体，而不是像过去那样把中国人和中国政府分开，区别对待。<br />
<br />
　　阵营敌对最无理性存身之地，双方都会采取简化的认同，如足球流氓那样聚在一起羞辱对方，不问理由，没有是非。一旦当西方民众和媒体认定中国人对西藏普遍存有殖民主义心态时，会使他们认定西藏必须从中国统治下解放，而不管中国政权性质将来发生什么变化。中国民主人士坚称中国民主后西藏就会自由的许诺不会得到信任，因为制度变化不等于民众心态会变化。这将大大增加未来中国处理西藏问题的难度。<br />
<br />
　　今日中共不再是意识形态至上的革命党，而成了善于投机的实用主义利益集团。照理说从自身利益出发，它也应该避免与西方形成阵营对立。然而事情朝什么方向发展，往往取决于内在逻辑。专制体制的特点正是即使每个局部都以自我理性行动，全局结果却可能远离理性，而且也不符合全局的整体利益。这种局部理性合成全局非理性的“纳什均衡”，对事物走向往往更有决定性。下面我们会看到，正是“反分裂”官僚集团对自身而言的理性算计与趋利避害，合成了中国官方在此次西藏事件中的整体谬错。<br />
<br />
<br />
二、帝国政体的困局<br />
<br />
<br />
1、当“反分裂”成为饭碗<br />
<br />
　　帝国政权最重要的职能是保持帝国疆土的完整，所设立的“反分裂”部门因此地位显赫。曾担任过中共高官的西藏共产党创建人平措汪杰先生对这些部门与人员的描述是“吃反分裂饭、升反分裂官、发反分裂财”。这是因为，所谓的“分裂”越严重，帝国赋予这些部门和人员的权力就会越大，输送的资源也会越多。这决定了他们从本能上会希望“分裂”问题始终存在。为了部门利益和个人利益，不但不要民族问题最终解决，反而会有意强化冲突，渲染“分裂”危险，无限上纲，没事找事，小事搞成大事；或是为了显示政绩，强化对事态的处置，促使冲突扩大；或是为推托责任，拒不承认错误，用更大的错误掩盖较小的错误。对官方在这次事件中表现的不智，有人归于行事官员愚蠢。其实那不对，官员们都很聪明。他们不是不知道自己的行为对整体不利，照做不误的原因在于他们的目的不是为了办好事情本身，而是为了自己从中得益。例如组织海外华人和留学生搞“红海洋”的驻外官员，很多都在西方受教育，长期生活在国外，不会不了解那种场面一定让所在国民众和媒体反感，破坏中国的形象。但是他们的官运不取决于所在国的好恶，而是取决于中国领导人和主管部门的满意。中国领导人在电视上看到西方国家布满中国国旗和爱国华人，感到脸上有光、心头解气时，那些驻外官员就有了增加升迁的可能。这才是他们的出发点。至于对国家造成的危害，那不会落到他们个人头上，也不由他们承担责任，因此无需他们操心。<br />
<br />
　　前面说了涉藏官僚集团把事件起因推给“达赖集团”能让他们把责任推得最干净，然而至少对其中的公安、国安、武警等部门却不是这样。他们的职责本来就是防止“敌对势力”和“境外势力”破坏，竟然让“达赖集团有组织、有预谋、精心策划”如此得逞，责任本该最大，为什么会默认这种不利于本部门的说法呢？除了为利益共同体所进行的必要担待，由此还显示官僚集团高超的另一面。单纯推卸责任只是官僚的初级本能，更高手腕则是把危机变成捞好处的机会。那些情治和镇压部门不会由自己担待责任，而是会归咎于资源不够、经费欠缺、权力所受限制过多、政策过于温和等“外因”；当他们用强力把藏人的抗议血腥镇压下去，摇身变为“对敌斗争”的英雄，不但无过，反而有功，同时再次证明了只有强硬镇压才是有效法宝；这时再反过来总结以往资源不够、权力所受限制多、政策过于温和的“教训”，要求权力高端给予更多资源和权力，实行加强他们地位的强硬政策。一般来讲，这种手腕一定得逞，因为只要利用信息不对称去渲染事件的暴烈、“分裂”的危险，可以轻易吓住帝国的权力高端。而对帝国当权者来说，为了保持帝国疆土完整是从来不计代价的。<br />
<br />
　　可以预期，这次西藏事件在很大程度上会打断中国政权走向开明化的进程，那些力图使中国摆脱警察国家的改革也可能夭折。尤其是在民族地区，原本已经实行的缓和措施正在取消，软化的权力重新坚硬，并且再度集中。在“反分裂”和“反恐”名义下，中国民族地区的未来政局难免趋向法西斯化。这在很大程度上，正是官僚集团吃“反分裂饭”的结果。<br />
<br />
　　官僚无论在什么制度下都会追求自身利益，这并不奇怪。但是好的制度会把官僚追求自身利益合成好的机制，坏制度却会把官僚追求自身利益合成坏的结果。让聪明的官僚聪明地去做对政权和国家愚蠢的事，正是专制制度的弊端，也是专制终会垮于内部的原因。<br />
<br />
<br />
2、“官僚集团的民主性”<br />
<br />
　　不过，人们免不了奇怪：专制政权的特点是上级决定下级，虽然官僚追求自身利益或部门利益，但是高端当权者对下属部门不顾全局的行为，为何会听之任之，不进行干预和统筹领导呢？<br />
<br />
　　这里不设想专制者昏聩的情况，虽然在专制历史上，被虚假信息包围的专制者落入昏聩远高于其保持明智的可能性。然而更能反映中国权力体系本质的，却是专制者即使明智，也无法扭转下属部门共谋形成的局面。这是因为，在专制体制中，专制者的决策只有通过官僚体系进行具体操作才能贯彻。正是这一点决定了，专制权力对外虽可以绝对专制，对内却存在专制失效的可能。只有在不损害官僚集团利益的前提下，专制者才有对官僚集团一言九鼎、说一不二的专制性；而只要损害官僚集团利益，官僚群体就会共谋合作，利用拖延、扭曲、不了了之等手段，在具体操作过程中把专制者的决策架空，使之无法推动和落实。这时，专制权力的专制性就大打折扣，我把这种状况称为“官僚集团的民主性”。<br />
<br />
　　无论是在古代王朝还是近代的专制政体，“官僚集团的民主性”都广泛存在，它不会体现于制度和程序，只是以官场潜规则的方式发挥作用，是一种物竞天择演化形成的机制。官僚之间在具体问题上有竞争，总体却是相互联合的共同体。他们对自身利益的得失极其敏感，精于谋利，容易形成共识；而且无需串联和组织，利用现成的官僚体系就能默契地共谋，达成相互庇护，成本低，风险小。因此，专制者要想让自己权力得到贯彻，便需要顺应这种“官僚集团的民主性”，满足（或至少是不触犯）官僚集团的利益，才能使官僚集团成为得心应手的工具，而不是阳奉阴违的对抗者。<br />
<br />
　　当然，历史上也出现过力图对官僚集团说一不二的专制者。那除了需要具有特殊权威，还必须使用极端手段――如启用野心勃勃的新人充当酷吏，对官僚集团进行清洗，以肉体消灭的恐怖进行慑服。然而那种手段只是见效一时，飞扬跋扈的酷吏几乎都无好下场。暴君终归要死，或是清醒过来就会明白：以利益收买而不是恐怖威慑更能换取官僚集团的效力。对于多数只求利益和权力的专制者，达成与官僚集团的分肥，自己清闲地坐车，让官僚们奋力地自觉拉车，岂不比与官僚集团南辕北辙地较劲要省心和实惠得多吗？<br />
<br />
　　即使以毛泽东对中共官僚体系的权威，在他要开展触及官僚集团的运动时，甚至无法在他居住的北京发表一篇文章，可见官僚集团的能量有多大。毛最终采取的方式是越过官僚集团，直接号召底层民众造反，摧垮阻碍他意志实现的官僚集团。那种被命名为“文化大革命”的方式是史无前例和登峰造极的，最终却归于失败，原因就在于毛也离不开官僚集团。文化大革命可以摧毁旧的官僚集团，但仍然需要建立新的官僚集团来为他贯彻权力。而不管什么官僚集团，最终都会演化成利益集团，“官僚集团的民主性”也就会起作用。毛泽东最终只能无奈地退到以“七八年再来一次”的恫吓，把他的文化大革命降低到酷吏来使用。<br />
<br />
　　在文革中吃了大亏的官僚集团就此留下刻骨铭心的记忆，他们决心不允许再出现专制领袖可以摧毁官僚集团的情况。毛后几十年中共推行“党的建设”、“发扬党内民主”等，说穿了就是在加强“官僚集团的民主性”，保护官僚集团不再受领袖伤害。而中国改革的主要内容――权力下放，本质也是让官僚集团分掌更多权力。至今，中共内部已经形成了完整的制约机制，官僚集团也有了相当充分的能力；既不允许再出现酷吏式的党内清洗，也不允许再有文化大革命那类群众运动，甚至不再允许党内出现可能导致分裂的路线斗争。今天，中共党内的高层权斗比历史任何时期都弱，权力交接也有了一定程序化，其深层原因，就是这种“官僚集团的民主性”在起作用。<br />
<br />
　　这是一种本质性的转变。在意识形态至上的时代以诉诸路线斗争进行的高层争权中，官僚们只能处于被动状态，选择跟随不同路线的代表人物，然后随代表人物的沉浮而沉浮。而当维护执政和获得利益成为中共的主要目标时，执政的具体操作者和利益的主要承载者――官僚集团就变成了党的主体。失去了意识形态背景的党内斗争变得名不正言不顺，从而不再有合法舞台。随着“官僚集团的民主性”进一步加强，不利于集团整体的党内斗争将在相当程度上被联合起来的官僚所遏制。这时的中共将变得比以往更少分裂，更为务实。高端当权者不再是以往那种凌驾于官僚集团的独裁者，而是变成了官僚集团利益的协调人与代言人。这时的高端当权者只有通过为官僚集团争取利益最大化，才能交换官僚集团的支持，坐稳自己位置。<br />
<br />
　　官僚集团熟悉政权机器，善于经营，一旦建立了制约高端当权者的机制，便一定会将其尽运用到极致，他们可以在无形中决定高端权位的起伏、人事更替、政策导向等。而只要他们具有了这种能力，就不会仅限于避免发生党内清洗或文革，而是扩展到避免对自身的任何不利，以及尽可能地用于为自身谋取更多利益。因此，把所谓的“党内民主”看做是中国民主的步骤，完全是看走了眼。“党内民主”只是古已有之的“官僚集团民主性”的别称，同中国民主与人民民主毫无关系。<br />
<br />
<br />
3、西藏问题何以无解<br />
<br />
　　明白了“官僚集团的民主性”及其在中共党内的作用，就可以看出中国现行的西藏政策为何不会改观。它牵扯十三个省部级以上的涉藏部门，或者说二十四个省部级以上的“反分裂”部门。了解中国情况的人都知道，采取“瞒上欺下”、“上有政策下有对策”、“一个拖字定乾坤”等手段，哪怕只有一个省部级单位不执行中央决策，中央无可奈何的情况都不鲜见，何况二十四个部门结成联盟。<br />
<br />
　　相对于毛泽东和邓小平，今日中共领导人的权威性无法相比，既缺少资历和功绩，也没有卡里斯玛人格（依靠特殊魅力和超凡品质吸引追随者的能力）。他们皆为官僚出身，在官僚体系浸淫多年，熟知官场规则，从逆向淘汰的官僚阶梯循序爬到高位，何尝不知道官僚集团的手段厉害？他们是否想走不同路线且不论，即使想，很可能也得知难而退。他们本质上不是领袖，只是到达了顶峰的官僚，几乎不会有超越性，全部目标就是掌权，不越雷池一步。因此，在毛、邓那类独裁者身上可能发生的决断与自我转变，对他们难以指望。这次西藏事件发生后很长时间看不到中共高端的动作，全凭“反分裂”部门自行处理，成为此次事件的特殊之处。二十四个“反分裂”部门涵盖权力的各个方面，印把子、枪杆子、笔杆子一应俱全，相互构成决策、执行和协作的完整链条，即使没有高端领导人主持，也能自我协调，自行运转。<br />
<br />
　　除此之外，今日中共还落进了一个自设的陷阱。从革命党蜕变成权力集团，作为权力核心的主权以及维护主权的民族主义成为唯一具有实质性的意识形态；举国体制进行的灌输与不厌其烦的改写历史，使中国人普遍确信当年中国以帝国方式占领的领土“自古属于中国”。这种状况使得“反分裂”官僚们占据了“政治正确”的位置，拥有民意支持，形成尾大不掉之势。“反分裂”既可以被他们当作盾牌，又可以被他们当作武器――无人敢对他们表示异议，他们却随时可以发动进攻。二十四个“反分裂”部门的联盟，加上占据了唯一具有实质性的意识形态高地，使他们能量十分巨大，甚至有能力启动“党内民主程序”进行政变，以捍卫国家主权不力的名义罢免跟他们不一致的高端领导人，而且很容易赢得党内党外的民意支持。因此，把权力视为一切的中共领导人在这种威胁面前，为了不被政敌抓住把柄，避免民众舆论的攻击，总是会宁左勿右，而不会采纳可能使自己地位受威胁的新思维。即使他们心里清楚如此下去会有更大危机，能做的也只是把危机发生时间尽可能后推。中共领导人从终生制改为轮流坐庄制，表面是一种进步，实际却导致在台上者宁愿“击鼓传花”，只保自己任内平安无事。至于把危机推到后任爆发，那就与己无关了。<br />
<br />
　　因此，国际社会敦促中国领导人与达赖喇嘛会面，达赖喇嘛希望绕过中间层次直接与中共领袖交流，藏人精英给中共高层写信恳劝，在我看都是没有认清真正的决定因素。西藏问题如何解决，并非中共高端可以独断。虽然在特殊情况下，中共高端可能对西藏问题进行某些特殊处置，但那只会停留在策略层面，不会触动实质。知道了“反分裂”势力在中国权力结构中占有多么重要和广泛的位置，就可以清楚：指望靠中共领袖个人（即使是再开明的领袖）解决西藏问题，是不切实际的幻想。<br />
<br />
<br />
三、西藏如何走向独立<br />
<br />
<br />
1．流血与独立<br />
<br />
　　“反分裂”官僚集团堵塞了在中国内部解决西藏问题的出路，持续产生的矛盾只能不断积累和发酵，最终把西藏推到争取独立的方向。不过，很多人会认为西藏与中国实力相差太过悬殊，即使西藏全民族都为独立而战，也不可能达到目标。如果孤立地看西藏和中国，并且静止在目前的力量对比上，的确是这样。然而在全球化时代，西藏和中国都要很大程度受国际影响，中国的强大也非永远不变。近年独立的东帝汶和科索沃在力量对比上都处于相当弱势，却都实现了独立目标。与他们相比，西藏独立的可能性不会更小。<br />
<br />
　　东帝汶和科索沃之所以能够实现独立，决定因素是国际社会――尤其是西方国家――的支持。之所以两地独立得到这种支持，很大原因在于两地人民遭受的人道灾难（至少桌面上的理由如此）。在人权高于主权成为国际社会共识时，种族矛盾导致的仇杀会成为民族独立被认可的主要根据。西方社会早就认为西藏发生过大规模人道灾难――藏人非正常死亡人数被认为接近百万，西藏寺庙曾遭尽数摧毁……在这种基础上，未来西藏发生的人道灾难即使没有东帝汶、科索沃那样惨烈，也足够让西方支持西藏独立。<br />
<br />
　　西藏未来会不会再发生人道灾难？可能性是很大的。这次西藏事件被镇压下去，却不等于就此了结，反而成为下一次更大爆发的起因。如同这次事件比一九八零年代的拉萨事件规模更大，下次爆发会比这次规模还大。这并非是无根据的猜测，我甚至能预言下次爆发：把其他可能的爆发撇开，至少在达赖喇嘛去世时，如果西藏问题仍未进展，达赖喇嘛也没能回到西藏，大爆发是一定躲不过去的，境内藏人会全面起事。中国镇压机器根本无法防范，因为信息时代已无法封锁达赖喇嘛去世的消息，而藏人起事也无需其他串联，消息本身就足以让各地藏人不约而同揭竿而起。<br />
<br />
　　领袖去世引发大规模民众抗议，中国有过一九七六年周恩来去世导致的四五运动，有过一九八九年胡耀邦去世导致的六四事件，其震荡都导致中国历史发生改变。设想达赖喇嘛去世引发西藏大动荡决不是危言耸听。稍微了解西藏就会知道：达赖喇嘛的命运是每个藏人心头的伤口。作为西藏宗教至高无上的领袖、活在现实的菩萨，达赖喇嘛放弃了西藏独立，只求以高度自治保存西藏文化和宗教。如此谦卑换得的却是羞辱，至死不能回到家乡，不能和终生等待他的人民与信徒见面，就此永别，这种痛是无可比拟的。达赖喇嘛在世时，不论有什么挫折，藏人总还有希望。一旦他去世，希望变成绝望，仇恨压过恐惧，爆发一定非常激烈，“西藏独立”也将从目前的口号成为切实的运动。<br />
<br />
　　当然，仅靠示威不能实现西藏独立，毕竟中国军警总数接近藏人的总人口，而且是以毁灭性武器对付赤手空拳。但是悲愤的藏人绝非轻易可以镇压，所谓“一人拼命，十人难敌；万人必死，横行天下”，何况藏人有六百万。那时中国当局将不得不再次乞灵于种族主义（信仰崩溃的中国再无其他意识形态），煽动以汉人为主的中国军警大开杀戒，同时鼓惑中国民众支持“反分裂”的屠杀。<br />
<br />
　　很多藏人会在那个过程失去生命，但是民族独立之路往往正是鲜血冲刷出来的。镇压不一定会使藏人退缩，他们甚至可能主动迎接暴力。一方面西藏传统宗教的苦行观念会提供这种动力，一方面非暴力抗争也有这种内涵。流亡藏人居住的印度是非暴力抗争的发源地，被誉为圣贤的甘地正是号召他的追随者要“学习死亡的艺术”。按照一些分析者的解读，甘地主张的并非是对施加于己的暴力不反抗的“消极非暴力”，而是要去主动挑起对方的暴力施加于己，才能“通过自己承受苦难达到改革的目的”（甘地）。对持有这种理念的西藏独立者而言，中国对藏人的屠杀正是能够获得国际社会支持西藏独立的历史机会，因此不是要力图避免，反而应该有意加以运用；甚至会期望中国的屠杀够得上种族清洗的规模，才会调动起国际社会最大的同情与支持。从西藏独立者角度，这并非是无视藏人的生命，而是在实践甘地所述的非暴力抗争之核心――“把生命奉献给自己认为是正当的事情”。藏人的血流得越多，国际社会对西藏独立的支持就会越强。由此来看，中国的武力镇压不但将是失效的，而且起到的是完全相反的作用――是在帮助西藏实现独立。<br />
<br />
　　发生种族流血冲突和出现种族清洗的灾难，是种族间无法共存的证明，是促使国际社会出面对受迫害民族进行庇护、继而支持受迫害民族建立独立国家的主要理由。而按照中国“反分裂”官僚集团推动的逻辑发展下去，未来西藏难以避免发生这种灾难。那时，西藏实现独立所差的就只剩最后两项条件：一是西方国家出面推动西藏独立，并且承认西藏的国家地位；二是中国自身陷入动荡，不再有对抗西方和镇压西藏的能力。<br />
<br />
<br />
2、西藏是西方的政治正确<br />
<br />
　　至今，西方政府对中国继承帝国遗产并未表达异议，也没有公开支持西藏独立。在西藏问题上与中国对立的，主要是西方民众与媒体。西方社会是多元的，几乎在每个问题上都存在争议，然而在西藏问题上却是空前一致。对中国人来讲，这可以算一种奇观――很多西方媒体完全不了解西藏，却总是黑白分明地下结论；更多的西方人完全没见过西藏，却要充当西藏的代言人和捍卫者。中国人的质问是：即使中国继承了帝国遗产，曾经欺侮过中国的西方列国继承的帝国遗产更多，甚至今日世界很多国家都是当年西方殖民者的后裔所建，西方人有什么资格在西藏问题上装成正义化身指责中国呢？<br />
<br />
　　时间是一种解释。殖民时代已经过去，西方当年的错误不能作为中国今天的理由。的确，时间需要成为一个因素，否则任何根据都会在无限追溯中无从立身。中国和西方几乎在每一步上都有时间错位：中华帝国腐朽的年代，正是西方列强最为凶悍之时；西方殖民行为获得合法性，主要靠有效统治，中华帝国时间虽长，却只求万方来朝的心理满足，放任“诸藩”自成一体；当与西方的相遇使中国明白“朝贡体系”只是虚荣，必须实现有效统治才能符合主权体系、保住疆土，一连串的国难和战争又阻碍了这种进程；而当世界进入非殖民化时代，民族独立建国成为主流，西方国家纷纷从殖民地撤出时，中国却以前所未有的强势进军西藏，导致达赖喇嘛带领数万藏民流亡他国；随后当民主、信仰自由和文化多元在西方成为文明进步的原则时，中国却在西藏开展了砸毁寺庙、禁绝宗教的文化革命；今天，当人权高于主权在西方世界成为普世价值观，中国仍然以主权为至高无上，为此肆无忌惮地侵害人权。<br />
<br />
　　不难理解，首先是这种时间错位，使得每一步都与潮流相逆的中国在西藏问题上饱受西方指责。不过中国人仍然可以反问：凭什么由西方确定时间标准？凭什么要把西方当作原点判断中国的错位？即使从中共执政算起，至今中国已有效统治西藏半个多世纪。而今天西方的法国、英国、加拿大、西班牙等都存在民族问题，为什么只有中国的西藏问题被指责？<br />
<br />
　　这就涉及到西方与中国的另一个不同――西方国家是用民主方式赋予了保留帝国遗产的合法性。如科西嘉岛百分之八十居民表决留在法国，比法国本土居民希望保留科西嘉岛的比例还高百分之二十（不少法国人认为科西嘉岛是负担，不如让它独立）；而英国当年允许北爱尔兰自主决定去留，结果是多数北爱尔兰人选择留在英国；西班牙的巴斯克、加拿大的魁北克都是经过民众公决否定了独立。因此，那里的民族问题只是少数人进行独立活动。然而对西藏却不能这样说，因为至少中国从未给过西藏人民选择的机会。<br />
<br />
　　今日，在摒弃了殖民主义的西方社会，自由、人权、反殖民成为社会共识，而民主制度则使西方民众对中国拥有居高临下的优越感。普通的西方人不像政客或商人受到中国的利益或政治制约，他们判断远离日常生活的西藏问题，主要是用价值标准。西藏在他们眼中一直是被中国占领的独立国家，而西藏问题几乎能综合所有西方与中国的价值分歧，因此顺理成章地成为炮轰中国的最好靶子。不管中国人对此如何不解与愤怒，这已经成为中国未来必须面对的一种现实。<br />
<br />
　　对此，西方媒体起的作用也很大。一方面，市场化的媒体必须跟随大众感情在西藏问题上追随大众一面倒毫不奇怪。另一方面，当大众与媒体高度一致地相互作用时，会产生一种趋于极端的性质，我称为“广场效应”。如同希腊城邦在广场上进行的民主，竞争者把煽起群众狂热视为成功，群众则以鼓掌或喝倒彩方式表达支持或反对那样。今天的公众被媒体和当代通讯技术联系在一起，等于制造了一个可以无限放大的广场。媒体与大众的相互激励，汇聚出比古代广场大得多的群众效果，且导致情绪在整个社会快速地传染。<br />
<br />
　　大规模人群彼此无法充分沟通，也无法进行深入思考，只能用简单概念获取共识，以明确结论断定是非。西藏独立在西方本来就有很强的民意支持，这次西藏事件中形成的中西阵营对立，中国民众的反西方姿态和对西方媒体的围攻，进一步加强了西方社会在西藏问题上的“广场效应”。市场竞争促使西方媒体紧盯大众关注热点，因此今后会始终把西藏当作对象，不懈地捕捉任何蛛丝马迹，特别是藏人的抗争和中国的镇压，并且把所有的迫害和流血都以放大方式展示给西方民众。那种效果反过来又会给“广场效应”不断加码，促使西方民意更加支持西藏争取独立。在当今世界所有民族问题中，最能得到西方社会支持的就是西藏。可以说，西藏自由已经成为西方社会的一种政治正确，同时也成为各种势力争抢和利用的制高点。<br />
<br />
<br />
3、西方政府的考量<br />
<br />
　　如果与中国对立的仅是西方民众与媒体，只在乎权势的中国当局尽可以当作“杂音”不予理会。然而中国当局所在乎的西方当权者，却是要靠民众的选票才能上台。他们只要追求选票，首先就得追随民意，因此最终一定会被民间的“广场效应”裹挟。这是西方民主制度的基本状态，本质上是不可更改的。面对西方，中国政府无法要求西方公众改变态度，也无法要求西方媒体转移立场，那只能得到反感和反弹，自取其辱。中国政府能做的，顶多是用贸易大单引诱或威胁西方政府，迫使西方政客们接受中国的立场。但是即使西方政客一时顺从中国，却不能像中国政府那样管束本国民众与媒体。相反，他们归根结底要被本国民众和媒体所决定，一俟时机成熟，就会争相站到支持西藏独立一边。<br />
<br />
　　虽然今天没有哪个政府承认西藏独立，然而政府态度从来是可以变化的。民间“广场效应”的推动只是因素之一，西方政府对西藏独立还会有另一面考量。六四时西方国家虽有过对中国的联合制裁，但那时针对的只是中国政府，对中国民众的民主追求却抱有希望。随后的苏东变化给了西方信心和耐心。邓小平推动中国经济走上资本主义之路，更使西方相信中国会随着经济变化出现政治变化。然而这次西藏事件证实了这种希望的虚幻。中国没有随资本主义进程融入西方阵营，反而随经济崛起有了更多威胁性；中国当局这次放弃了邓小平的“韬光养晦”方针，极力显示强大、强硬、以及操控民众的一面；中国民众（至少是西方眼中看到的）并没有成为民主力量，反而是和专制政府结成了同盟，对内欺压弱小民族，对外敌视民主社会。这样的中国完全是一个新法西斯帝国的形态，令西方政治家感到惊恐。未来若任凭中国崛起下去，会不会有一天威胁世界和平？<br />
<br />
　　在民主、自由、和平等表面文章之下，西方对中国的不满还与切身利益有关。譬如中国以低人权和破坏环境为代价吸引国际资本，制造出大量廉价产品冲击国际市场，损害了西方国家上百年才得以形成的劳资关系、福利制度和市场秩序；中国经济对资源的巨大饥渴给全球生态危机雪上加霜；中国日益活跃地参与争抢资源的行列，在全球与西方进行角逐，加剧了资源紧缺，导致价格飞涨。此外，更深层还有不会摆上桌面却绝非不重要的担忧――假如中国达到与西方相近的消费水平，濒于生态极限的世界会不会因此崩溃？西方政治制度决定了无法以降低本国消费解决生态极限的危机，只能去抑制其他国家――尤其是中国这种人口大国――的消费提升。这其中当然存在显而易见的霸权主义和不平等人权，却不是没有可能成为西方政府制定中国政策的潜在依据。<br />
<br />
　　为了上述桌面上和桌面下的考量，削弱中国、抑制中国继续发展也许有一天会成为西方国家的共同愿景。而达到这个目的，肢解中国，让中国陷于肢解后的内部冲突和相互牵制不能自拔，也让中国丧失支撑崛起的资源与空间，是釜底抽薪的方式。只要找得到名正言顺的口实，也许就会成为西方国家乐于接受的集体安排。而什么口实会比以反帝反殖民名义促成西藏独立更为名正言顺呢？西藏独立会连锁地带动新疆甚至更多中国民族地区独立。要知道，仅西藏和新疆两地，就将使中国失去百分之四十领土，也会把中国更远地隔离在欧亚大陆一隅。这种对中国的肢解不但不会遭受谴责，还会戴上解放者的桂冠。因此，目前看似小心翼翼不忤逆中国的西方各国政府，只要历史发展提供最后一个条件――中国陷入内乱，就可能在很短时间转移到支持西藏独立的轨道上。<br />
<br />
<br />
4、中国难以避免陷入内乱<br />
<br />
　　那么，今日看似日益强大、不断崛起的中国，会不会有陷入内乱的一天呢？一种对目前中国状况的评价是：从来没有过这么好，也从来没有过这么糟。不过多数人看到的中国仅是从未有过的好，没有看到从未有过的糟。而被看到的好和看不到的糟之间，其实只有一线之隔。很可能在人们毫无意识之时，从未有过的糟就会突然浮出水面，在措手不及中酿成大祸。此次西藏事件的爆发，原因之一正是在于以往只看到了“好”。<br />
<br />
　　其实，今日中国的好，说穿了只是经济发展速度快。且不说这种发展速度快的代价须在明日加倍偿还，仅以常识也可知世上没有任何经济能够永远高速发展。中国经济已埋伏种种隐患，面对重重困境，危机迟早会不期而至。那时，原本被经济高速发展掩盖的社会危机和政治危机必将随之而来。三重危机叠加，政权垮台、管治真空和社会动乱等一系列后果都可能出现。<br />
<br />
　　眼下，因为看不到挑战中共政权的力量，人们往往以为中共就会一直统治下去。然而大变化不是一定非得出自大力量或者大事件，细微的积累同样可以导致崩溃结果。有一种研究让沙子一粒粒落下，形成逐渐增高的沙堆，同时精确地计算每落一粒沙会连带多少沙粒移动。初始阶段，落下的沙粒对沙堆整体影响很小。但是当沙堆增高到一定程度，达到“临界”后，沙堆就具有了“一体性”。那时每粒新落下的沙都会产生一种“力波”，尽管微细，却能通过沙堆的“一体性”贯穿沙堆整体，将新落沙粒的碰撞传给所有沙粒。沙堆结构将随每粒沙的落下逐渐变得脆弱。说不定哪一粒落下的沙（注意：只是一粒沙），就会导致沙堆整体发生坍塌，也就是所说的崩溃。<br />
<br />
　　有一个西方谚语，说的是断了一个马蹄钉，绊倒了马，摔伤了将军，输掉了战争，亡了国家。那国家当然不是因为马蹄钉亡的。用沙堆理论解释，就是那国家的内部危机已经超过临界状态，马蹄钉断只是引起坍塌的最后一粒沙而已。中国也如同一个沙堆，各种变化和冲击不断落在上面，积累的结果迟早会使沙堆超过临界状态。而到了连马蹄钉都成为“不稳定因素”时，专制政权控制再严密也是无法防止垮台的，因为它不可能给每个马蹄钉都派上看守的兵。<br />
<br />
　　按照中共目前这种拒绝政治改革、不断积累各种矛盾的道路走下去，中国迟早会爆发全面危机。专制政权的特点之一就是无法预测，既无规律，也缺乏资讯，因此其变化总是“突变”。试问有几个人事先看到庞然大物的苏联帝国会垮台呢？它却一夜之间就分崩离析。镇压可以把崩溃往后拖，如同不断拍打沙堆周边可以使沙堆继续增高一样。但是那种增高不会无限，最终还是要垮，而且堆得越高，垮得越烈。经验告诉我们：大系统虽有很强的自我维护能力，却总是一垮就兵败如山倒。<br />
<br />
　　真正稳定的社会需要具有多重整合机制，除了政权，还有道德伦理、法治、完善的市场、国家化军队，以及宗教组织、民间社会、多个政党等。这种多元并存的机制对保持社会长期稳定不可缺少。就像多党制中执政党下台、反对党马上可以顶替那样，防止出现权力真空造成的社会失序。然而步入多事之秋的今日中国恰恰只剩依靠行政体系和警察手段的一党政权进行整合，其他整合因素都被政权吞食，不是变成政权的依附，就是被视为异己力量而“消灭于萌芽”，或是在重重压制下无法生长，因此都无力担负整体的整合。这样的社会一方面似乎没有挑战，异常稳定，一方面却存在巨大的风险――一旦有一天政权垮了，整个中国就会出现整合真空，由此陷入内乱，长期无法恢复秩序。<br />
<br />
　　可想而之，当那一天到来，目前靠武力压制的民族矛盾会立刻爆发，民族独立运动也将风起云涌。而专制中国的镇压能力却会随内乱瓦解，或是消耗于内斗。那些坚持大一统的中国民主人士所说的，未来中国的民主政府照样不会允许西藏独立，只会是一句空话。因为陷入了内乱的中国哪里会有民主政府？民主政府如何能从被中共消灭了其他整合机制的空白中产生？那时就可以看出中共把中国绑为人质（要亡一起亡）的后果――当中共失去了控制中国从而也失去控制西藏的能力时，中国也将不会有任何其他力量可以控制西藏。<br />
<br />
<br />
5、决战在西藏<br />
<br />
　　达赖喇嘛的放弃独立，谁都知道是一种面对现实的牺牲。未来如果有一天西藏可以实现独立，他真会拒绝吗？中共执政者不抓住时机把他的放弃独立尽快变成法律事实，而是置之不理，何其不智。对中共的政客谋士而言，中共垮台的前景是思维大忌，因此会闭眼不看，也不会去想达赖喇嘛作为西藏问题的钥匙，可以开门、也可以锁门的可能――如果有一天中国陷入内乱，西藏问题仍无进展，达赖喇嘛就有充分理由号召西藏重新争取独立，并呼吁国际社会给予支持。既然他允诺的放弃独立不被中国理睬，也就没人能指责这种转变是食言。那时，以达赖喇嘛对藏人的号召力和对西方的影响力，将对西藏独立发挥无可比拟的作用。他一人威力抵得上百万之师。<br />
<br />
　　中国和西方两种本质不容的政治制度，交叉在全球化时代，从根本上只能渐行渐远，价值观和利益的双重冲突致使双方不可避免要进行最后决战。而在浩荡的世界民主大潮中，失败一方只能是专制中国。西藏将成为全球最后一个专制大国的克星。人权高于主权的观念，已经给西方铸就了切入中国的刀剑，而西藏正好提供了发起决战的战场。现在谁也无法预见具体进程，那会受很多历史因素的影响。但是所谓决战很可能没有两军对垒，在经济和外交领域就见分晓。中国国门已经洞开几十年，实用主义的经济发展对外来资金、技术和市场的无度追求，把中国的生存命脉很大程度上交到了西方手中。这使得中国在面临存亡危机时，除了接受西方安排，很少有其他选择。<br />
<br />
　　这里只作为举例进行设想：比如中国内乱与经济危机同步，既然中国经济已和世界融为一体，要度过难关就得靠掌管世界经济的西方国家援手。而西方国家若是要求以承认西藏独立作为交换，急于从崩溃边缘和更大灾难中被挽救的中国政府（如果那时还有政府）即使再不情愿，可能也不得不答应。另一种可能是，那时的中国陷入类似民国初年的分裂，一方面根本无暇西顾，无力镇压西藏的独立运动；一方面力图成为中国主人的各方势力争相得到西方国家承认――那样才能在国际上合法地代表中国，并得到西方输入的资源。西方则可以合纵连横，进行交换――哪一方愿意在承认西藏独立的文本上签字，就会承认和援助哪一方。当年蒙古独立只得到苏联的支持，无论是国民党政府还是共产党政府都无法抗拒，只能接受。将来的中国面对的将是西方列强联盟，何以抵挡？平时高喊统一的各方势力为了成为中国主人，很可能会以各种动听理由（如拯救亿万百姓生命等）抢着在同意西藏独立的文本上签字。类似情景在民国初年曾多次上演。中共也一直对列宁以“给新生政权争得喘息机会”为由签订的不列斯特和约给予很高赞誉，而那和约让苏俄向德国割让了上百万平方公里领土，赔款60亿马克，丝毫不亚于中国失掉西藏。这种实用主义精神某种程度上正是中共的本质。<br />
<br />
　　中国的百姓则会更务实。这次西藏事件中与西方抗衡的只是一批居住城市、善于使用互联网、与媒体接近、在中国现状中得利的人，他们的声音被放大，占人口的比例却很小。对中国的普通民众（底层百姓，工人、农民、农民工、上访人等）而言，切身的公正、人权、自由和民主远比西藏归属更重要。只是他们没有发声管道，不被媒体关注，在专制高压下也最缺乏抵抗能力。不过，在中国首先发出“不要奥运要人权”呼喊的，正是黑龙江的失地农民。虽然立刻就被镇压，却足以提醒那才是观察中国的真实角度。一旦整个中国落入危机，人人生存受到威胁，起哄的民族主义立刻会烟消云散。如同当年的河南饥民欢迎带来了粮食的日本侵略军那样，遥远西藏的去留不是问题，重要的是尽快摆脱切身困境。因此只要未来中国的危机足够严重，同意西藏独立便不会受到太大反对。<br />
<br />
　　一旦被国际社会认可的中国政府签署了同意西藏独立的协议，就具有了国际法保证的效力。以后即便政府更换，再想重新占领西藏，除非那时中国有了与整个国际秩序和西方阵营对抗的实力，否则便没有希望。当代世界在各地反复演练过的国际监管形式――联合国介入、维和部队进驻、划分隔离区、进行国际援助等――将稳妥地保证独立西藏的安全。而只要西藏作为一个国家坚持数年，就会成为不可更改的既成事实，长存永续。<br />
<br />
　　路线图到此结束时，在终点回首展望，可以清楚地看到：如果有一天中国失去西藏，原因不是民主，恰是专制。中国那些把专制政权视为中国统一保证的“爱国者”们，号称宁要专制不要分裂。而这个路线图所展现的，却是截然相反的结论――正是专制，导致分裂。<br />
<br />
<br />
2008年于北京</div>

							
							
							
							
