																											<div id="postmessage_537353" class="t_msgfont">　　　　　　　　　　　　　　西藏独立路线图<br />
　　　　　　　　　　　　　　　　王力雄<br />
<br />
<br />
一、西藏事件是分水岭<br />
<br />
<br />
1、官僚集团成为主导<br />
<br />
　　这个路线图从分水岭出发。在2008年的西藏事件之前，我从未认真地对待过西藏独立。这个事件是一个分水岭，让我第一次正视西藏有了实现独立的可能。如果说此前西藏独立只是梦想的话，此后西藏独立却浮出现实的水面，进入了目力所及的视野。之所以发生这种变化，主要推动者不是别人，正是中国权力体系中担负“反分裂”职能的官僚集团。<br />
<br />
　　十九世纪中到二十世纪中的中国，一直是身受帝国主义之害的形象。近代中国人牢牢记住了那段屈辱，却很少有人去想中国也曾是一个帝国，在十七世纪到十八世纪进行过巨大的疆土扩张，虽有过被其他帝国打败和瓜分的屈辱，但至今仍然继承着相当一部分帝国遗产，包括西藏在内。<br />
<br />
　　今天，西藏领土占帝国版图的四分之一，西藏问题在帝国政治中有重要地位，中国的党政权力体系中有诸多部门与西藏有关，其中省部级以上的部门有如下十三个：<br />
<br />
　　1、西藏自治区<br />
　　2、青海省<br />
　　3、甘肃省<br />
　　4、四川省<br />
　　5、云南省<br />
　　6、中共中央西藏工作协调小组<br />
　　7、中共统战部<br />
　　8、公安部<br />
　　9、国家安全部<br />
　　10、军队<br />
　　11、武装警察部队<br />
　　12、国务院新闻办公室<br />
　　13、国务院宗教事务局<br />
<br />
　　这些部门都设有专门处理西藏问题的机构，有一批长期甚至终生行使这种职能的官僚。除了这十三个部门，还有以下十一个虽不直接涉及西藏，同样担负“反分裂”职能、设有反分裂机构与人员的（省部级以上）部门：<br />
<br />
　　1、中央政法委<br />
　　2、中共中央新疆工作协调小组<br />
　　3、新疆自治区<br />
　　4、新疆生产建设兵团<br />
　　5、内蒙古自治区<br />
　　6、外交部<br />
　　7、国家民族事务委员会<br />
　　8、国务院台湾事务办公室<br />
　　9、国务院港澳事务办公室<br />
　　10、香港中联办<br />
　　11、澳门中联办<br />
<br />
　　加在一起，中国的官僚体系中具有“反分裂”职能的部门共计有二十四个，乃是一个相当大的集团，拥有的权力、人力和资源非同小可。在处理这次西藏事件中，他们是一个联盟，主导了整个处理过程。这一点与毛时代和邓时代不同。当年无论是对西藏实行“统战”或“平叛”，还是“拨乱反正”或“戒严”，都是权力高端决策，官僚集团执行。而这次西藏事件，权力高端基本无所作为，完全由官僚集团自行处理。<br />
<br />
　　这不能简单认为只是高端下放权力。实际上，拉萨事件发生的当月，中国总理温家宝曾在老挝出席大湄公河次区域领导人会议时呼吁，希望达赖喇嘛能够施加他的影响平息西藏事件。这种前所未见的说法引起了国际广泛关注，被认为是中国领导人的新思路。然而此后不再有任何下文，“反分裂”官僚集团对事件的处理方式没有任何变化。由此可以看出，今日中共政权处理西藏问题，不仅不需要权力高端进行决策，甚至即使高端进行了决策，若不符合官僚集团的意图，也不会生效。这种局面很可能会成为今后的定规。至于为什么发生这种变化，容后再述。<br />
<br />
　　在权力高端能够独裁时，一方面会有专横、粗暴乃至荒谬，另一方面也存在高瞻远瞩、拨乱反正、突破和变局的可能。二者只是一步之遥，有时就在于当权者的一转念。而当官僚集团成为主导时，就不会再出现那种戏剧性，而是落进明确的规则框架。官僚集团是一种互相牵制、按部就班、墨守陈规的结构。更重要的是，官僚集团是一个利益主体，如果由它控制决策，所有决策都必须符合其自身利益。当其自身利益与社会利益发生矛盾时，官僚集团就成为破坏者，不仅破坏民众利益，也会破坏它本当为之服务的统治目标。“反分裂”官僚集团正是这样，在它按照自身利益去驱动“反分裂”的行动时，结果是把中国推向更危险的分裂。我们不妨从这个角度，看一下这次西藏事件的脉络。<br />
<br />
　　类似拉萨的“3.14”街头抗议和暴力事件，其实在中国内地屡见不鲜。内地处理事件的方式已经很糟，但如果对“3.14”事件采用与内地同样的方式――新闻封锁、大事化小、不激化矛盾、镇压加安抚、在下级官僚中抓替罪羊以平民愤等――很可能就不会发生随后波及整个藏区的连锁反应。然而对西藏问题，官僚们却不会有这种平常心。因为首先西藏被世界关注，事件会引发国际舆论；其次西藏动乱会使当过西藏主管的中共领导人胡锦涛难堪，层层官吏都怕被怪罪；其三，既然官方多次宣布“西藏处于历史最好时期”，出了这种自打耳光的事必须自圆其说。在中国官场，如果是由少数个人或单一部门承担责任的事，可以通过找替罪羊的方式处理，官僚之间的竞争也容易导致落井下石。然而西藏事件不是西藏一地之责，在花了那么多钱、用了那么长时间、下了那么大功夫之后，仍然出现如此规模的民众抗议，仅凭常识也会知道是治藏路线的失败。然而治藏路线是由众多部门共同推动和执行的，承认路线失败，十三个涉藏部门都脱不了干系，其他的“反分裂”部门也会受连带影响。这种责任是整体性的，面临的问责也无法靠官僚之间的推卸化解得了，诸多官员的仕途前景都会受影响。因此，“反分裂”官僚们必须团结起来，结成联盟，共同行动，帮助西藏的官僚推掉责任，才能让他们一块得到解脱。<br />
<br />
　　责任如何推卸得最彻底？可想莫过于推给“达赖集团”。因为不管什么理由，只要是出自西藏本土，就免不了官僚自身的责任。只有把责任推到境外，才能让自己完全无关。西藏自治区当局在拉萨事件发生当天就对新华社宣称：“有足够证据证明这是达赖集团有组织、有预谋、精心策划的”。这种宣称立刻被所有涉藏部门和反分裂部门当作统一口径，虽然他们至今也不能把“足够证据”展示于世，可见只是推卸责任的谎言。然而官僚们并不在意能否拿出证据，他们的目的是在第一时间主导舆论。这一点的确是被成功地做到――他们的谎言立刻变为中国舆论的众口一词，成了一种不容置辩的话语模式，起到了诱导和迫使社会（包括权力高端）遵循同样口径的效果。<br />
<br />
　　起点决定走向，起点失之毫厘，结果差之千里。这种出于推卸责任的官僚口径，决定了后续行动的框架，也决定了事态发展的走向。譬如3月14日拉萨发生闹事的区域曾出现数小时放任状态，外围已经集合的军警不进入、不行动，任凭暴力行为升级。很多人对这个奇怪现象困惑不解。在不同版本的解释中，我比较倾向于这种做法是为镇压“培育”理由，以及为了拿到“宣传攻势”所需的电视画面。一方面把摄制组部署进闹事地点完成拍摄需要时间；一方面暴力不受抑制时会不断升级，可以给镇压提供更充分的合法性，也容易拍到更有烈度的暴力画面作为证据。如果从闹事初起就动用警力控制，事件规模会小很多，有利全局，却不利相关官僚。因为事件既已发生，在较低烈度时镇压会引起非议指责，包括北京也可能不满，既不能让官僚有效地转移事件责任，还可能增加处置不当的罪名。因此他们宁可按兵不动，让事态扩大，等待事件达到充分严重的程度，可以被认为是“有组织、有预谋、精心策划的”之时，再进行镇压。那时外界无法指责，北京也难以怪罪。<br />
<br />
　　这就是专制制度的特点――权力体系中每个角色都把自身利益置于核心位置，以个人得失决定行动。官僚为了个人算盘，宁可把事情推向恶性发展的方向，不管会造成什么恶果。回顾此次西藏事件的脉络，从拉萨僧侣的和平请愿，一步步发展成遍及藏区的动荡与镇压，以及扩大到全球的抗议和反抗议，很多环节都有这个因素在起作用。<br />
<br />
<br />
2、民族矛盾变成种族对立<br />
<br />
　　3.14事件发生后，官僚们首先要向政权高端、国内民众和国际社会证明镇压的必要与合法。他们采取的方式是先发制人，利用一切舆论手段进行频繁的信息轰炸；同时封锁现场，阻断信息，不让对手掌握不利自己的证据，由此达到垄断舆论的目的。与中国其他地方发生类似事件时媒体几乎不报道、更少见诸电视画面相比，这次对拉萨出现的暴力却一反常态，十几个小时之后就通过电视新闻把画面送到全国乃至全世界。密集播放藏人对汉人的施暴，不谈事件起因，单一地表现成藏人攻击汉人（尽管存在这种攻击），归咎于境外操纵的分裂，由此煽动汉人针对藏人的民族主义情绪。<br />
<br />
　　民族对立是一个国家发生分裂的根本原因，本是首先应该避免的分裂，然而担负“反分裂”职能的官僚集团却在此次西藏事件中一手制造这种分裂。他们不是不知道后果，而是十分明白地就是要利用这种对立，需要的就是这种后果。只要煽动起中国主体族群――汉人的民族情绪，形成同仇敌忾的社会氛围，就可以让他们躲在后面，不但可以避免来自民意的质询和追究，还能借助汹涌的民意把权力高端纳入他们的轨道。任何对阴谋论的怀疑、对己方的检视、力图温和的处理方式、缓和矛盾的用意，都会在激愤的民意威慑下无法伸张，只剩“反分裂”官僚的谎言成为不容怀疑的定论，不断放大，裹挟所有声音和行动。<br />
<br />
　　这种宣传不会平息事件，反而是火上浇油。一九八零年代的藏人抗议局限在拉萨，这次却遍及整个藏区，其中电视是重要原因。当年少有的电视现在普及到各个角落。虽然藏人攻击汉人的画面可以让汉人认同对藏人的镇压，但是对藏人的效果却相反。各地藏人在日常生活中积淀的不满，很容易被直观的电视画面引发出来。可以说，电视在相当程度上成了引发藏区抗议的动员令。作为藏人，不但理解和同情拉萨街头发生的行为，有些人还会被电视内容误导，认为应该用同样行为表达不满。藏区个别地方发生针对异族的暴力事件，正是在看到拉萨街头的电视画面后。当局把藏区各地同时发生抗议作为是“有组织、有预谋、精心策划”的证明。其实哪里需要组织、预谋和策划，让各地藏人同时看到拉萨人已经起来的活生生画面，就足以让他们同时走上街头了。而这个动员令，正是当局自己发出的。<br />
<br />
　　另一方面，单方面取舍材料、不探讨理由只渲染现象的舆论煽动，把事件片面表现为藏人对汉人的无端仇杀，造成的结果是汉人与藏人的种族切割。近年汉人对藏文化表现出的向往和亲近，发生一百八十度转弯，变成了对藏人整体的畏惧和仇视，把藏人视为恩将仇报的民族。互联网上充斥中国愤青对藏人的狂热辱骂，对藏人的排斥成为中国社会弥漫的风气，到处发生对藏人的歧视与不公。机场、旅馆、各种检查站，只要见到藏人，即使是位居高官者也免不了侮辱性对待。甚至藏族儿童在学校也受汉族同学欺侮。这种出自汉人的整体敌对，把藏人整体推到了汉人的对立面。出于对官方宣传的反感，藏人对官方言辞一概抵制，同时也对汉人的仇藏还以相反的仇汉。可以说，从这次事件后，汉藏之间已经形成了以血缘划分的种族对立。最典型的例子是在随后举行的北京奥运会期间，本无民族区分的藏族孩子从以往为中国加油变成每见中国失掉金牌就欢呼。孩子的变化，正是预示西藏问题的长远走向。<br />
<br />
　　在这次西藏事件前，西藏独立的其他条件一直相当充分――单一民族、单一宗教和文化，地域界限分明，历史地位清楚，国际社会高度认可，有众望所归的领袖和运转多年的政府……然而不具备的是最重要的条件，即作为藏人主体的境内藏人缺乏追求独立的足够动力。虽然西藏问题长期存在，但是集中于政治、历史或文化层面，双方被卷进纠葛的主要是官方、民族上层、知识界或国际社会。即使是一九五零年代的藏人武装反抗和达赖喇嘛出走，也被毛泽东化解为阶级斗争，并未变成种族对立。一九八零年代的拉萨抗议因为没有扩散到其他藏地，也未影响到整体的藏汉关系。两个民族的普通民众之间，可以说此前关系一直比较和睦，甚至比较亲近。在境内藏人多数不追求独立时，有利于西藏独立的其他条件再多，也不会发生作用。我过去之所以没有正视西藏独立前景，原因正是在这里。<br />
<br />
　　但是这次西藏事件却在藏汉民族之间整体地切开了鸿沟，民族关系变成了种族对立，情况就发生了本质变化。上层和精英之间的民族矛盾相对容易化解，政策变化、体制改革、翻案平反等都可能成为出路。然而以血缘划分的种族矛盾涉及每个人，遍布在两个民族日常交往的所有细节，双方任何成员、任何接触都可能成为冲突之源。而所有冲突都会起到继续动员的作用，使得种族之间仇恨增长，冤冤相报，冲突升级，两个民族只能越走越远，从此踏上不归路。这时受到压迫与歧视弱势民族一方，产生要求独立的诉求几乎是顺理成章的。一旦境内藏人普遍把西藏独立当作愿景，西藏原本具备的独立条件就立刻变得有效。正是这个重大变化，使得西藏独立进入了现实层面。虽然最终能否实现独立还要取决于历史时机和外部环境，但至少从藏人自身而言，条件已经齐备。这是此次西藏事件完成的转折。如果“藏独”方面要论功行赏的话，头功理所应当该授予的就是把藏汉民族变成了对立种族的中国“反分裂”官僚集团。<br />
<br />
<br />
3、预期的自我实现<br />
<br />
　　既然从一开始就把事件定性为“达赖集团有组织、有预谋、精心策划的分裂活动”，对于把统一奉为最高原则的政权，处理方式就只能是坚决镇压，没有缓和余地。这是藏区各地政府和官员不敢有违的立场，也是军警执行镇压的指导思想。拉萨事件发生后，藏区各地政府及军警的过度反应与草木皆兵――大规模逮捕、暴力镇压、刑讯逼供、封锁寺庙、迫害僧人、人人过关等，引起藏人广泛不满，促使更多普通百姓卷入其中，致使反抗扩散到整个藏民族。应该是这次西藏事件规模如此扩大的另一主要原因。<br />
<br />
　　在官方宣传灌输下，从外地调进藏区执行镇压的汉人士兵把藏人一概视为分裂国家的敌人，因此以仇恨心态对藏人施暴，引发了很多本来不该发生的冲突。比如汉人士兵见到藏人家里供奉“分裂集团头目达赖”的照片，会粗暴地进行损毁，甚至逼迫藏人自己动手损毁。这对把达赖喇嘛视为至尊的藏人是不可接受的。如果藏族老人为保护达赖喇嘛像而遭士兵殴打，老人的子女当然愤怒，亲属和乡亲也会不平，于是卷进更多的人。冲突就这样发生和扩大，演变成事件，甚至导致军警开枪，造成伤亡，再被归于“有组织、有预谋、精心策划的分裂活动”进行镇压。类似之事这次各地多有发生，其实往往并无政治内涵，完全属于“激起民变”。<br />
<br />
　　1989年六四事件后，中共总结的“要把一切不稳定因素消灭在萌芽状态”，至今已化为官僚集团的基本思维，是所有官僚奉行的准则。按照他们的权力崇拜心态，只要有权力，一切都可以恣意妄为。他们在民族地区实行的政策是“主动出击、露头就打、先发制敌”，后来发挥成“不露头也要打，要追着打”。这种穷凶极恶在这次西藏事件得到充分体现。本来许多和政治无关的活动，如过节、赛马、宗教法会等，只是传统风俗，自古已有。但是在把“非我族类”视为“其心必异”的官僚眼中，尤其是外地调来、全然不了解民俗的军警来看，只要聚众就可能出事。既然要“先发制敌”、“不露头也要打”，最稳妥的就是禁止任何形式的聚众，封杀所有民间活动。或者即使没有禁止，也要重兵防范，军警包围，架设武器威慑等。可想藏人对此的反应――“凭什么你们能开奥运会，我们不能开赛马会？！”血气方刚的藏人面对如临大敌且态度蛮横的军人，一言不合就可能引发冲突，酿成事件。对当局来讲，那正好验证了聚众就会出事的判断，于是更加禁止民众活动。孰不知之所以出事，恰恰就在于他们的这种判断。<br />
<br />
　　其实，即使从统治者角度，把全部矛盾“消灭在萌芽”也非好方法，因为“萌芽”不能真实地显露事物性质。有些“萌芽”本来并非“不稳定因素”，让其生长只能有利于稳定，而粗暴的“消灭”却将其推到了敌对，等于制造了新的敌人。即使这种镇压一时造成稳定的表面，但是从长久看，不稳定的因素不会被消灭，只是压抑和积累，等待时机形成更大的爆发。<br />
<br />
　　僧侣在藏地本是理性与和平的群体。当他们以和平请愿的方式表达不满，如果当局能够虚心聆听，正面互动，对藏地的长远稳定肯定只有好处。然而当局内心深处把僧侣视为不劳而获的寄生虫、达赖扎根西藏的基础、西藏独立的土壤、闹事者和煽动者，总之都是负面的，因此一遇到僧侣挑战，就会条件反射式地做出粗暴举动。3.14拉萨街头出现的暴力事件，与此前连续几天军警殴打和平抗议的僧侣有直接关系。那几乎是一模一样重复1987年拉萨事件发生的起因，让人惊讶当局不汲取教训的颟顸。对藏人稍有了解就会知道，与官僚对僧侣的蔑视与厌恶相反，僧侣在藏人心目中地位极高，既是藏传佛教的三宝之一，又是藏文化的传统知识分子，作为藏人精神世界的引导者和护佑者，受到藏人极大尊崇。因此藏人最不能容忍僧侣遭受虐待和羞辱。军警对僧侣的暴力行为会引发藏人骚乱几乎是必然的，只有被权力傲慢遮蔽了双眼的帝国官员才会看不到这种后果。<br />
<br />
　　而当局并未反思，反而变本加厉。各地僧侣被当成主要打击对象；众多具有崇高地位的寺庙遭到军警侮辱性搜查；除了参与抗议的僧侣大批被抓，还有很多僧侣被限制行动自由；一些寺庙长期被关闭；外地户口的僧侣被驱赶；强令所有寺庙开展所谓“爱国主义教育”，逼迫僧侣们公开谴责达赖喇嘛。很多僧侣为了躲避这种人人过关离寺出走，有些寺庙竟走得几乎无人。如果说在这次西藏事件发生前，僧侣中还有不少是不问政治、一心修行的，不满只停留在政策层面，并未导致整体否定中国的统治。这次事件却使西藏僧侣集体地转向对西藏前途的政治思考，赞成西藏独立的比例大幅度增加。<br />
<br />
　　中国当局把西藏僧侣整体地推到敌对位置，等于是给自己制造了最难摆平的对手。西藏传统民歌这样形容僧侣：“立起是一支香，倒下还是一支香。抓我的脑袋只有头发，摸我的屁股只有破布。”说的就是僧侣无家庭牵累，无后顾之忧，因此无所畏惧，敢于反抗和挑战。之所以西藏历次事件总是僧侣打头，这是重要原因。同时，以僧侣在西藏民众心目中的地位，以僧侣在西藏民间的广泛渗透，他们对中国统治的不满和西藏独立的诉求绝不会仅限于自身，而是一定会对藏人百姓产生广泛影响。<br />
<br />
　　官僚集团的另一种行为――急于拿出事件是“达赖集团有组织、有预谋、精心策划”的证据，为此大量抓捕藏人，使用刑讯逼供、制造冤假错案等，在这次西藏事件中也波及众多藏人及其家属，造成广泛的离心离德。在实施了众多迫害后，最终也没有拿出自圆其说的证据。媒体对达赖喇嘛的指控在藏人听来全是谎言，即使对未发生抗议的藏区，也使民众心生反感，造成扩大矛盾和制造仇恨的效果，反而会让更多的藏人考虑“分裂”是不是更好。当局进行的“反分裂”宣传等于在给分裂意识提供素材。藏语中的“西藏独立”――“博让赞”原本是多数藏族百姓不知道的词汇，也没有这个概念，但是经过长期的“反分裂教育”，这个词现在变得家喻户晓，妇孺皆知。在这次西藏事件中，“博让赞”成了从僧侣到普通市民到农牧民甚至小学生都要喊的口号。<br />
<br />
　　这就是所谓“预期的自我实现”――把藏人当成敌人，藏人最终会真成为敌人；处处防范藏人“分裂”，藏人最后就会真要分裂。对这次波及整个藏地的抗议运动的性质，观察者有不同解读。其中主要分歧在于：这究竟是要求西藏独立的政治运动，还是仅仅在表达对经济地位或官方政策的不满。让我来看，这次事件的过程不见得有明确的西藏独立诉求，而是诸多因素综合所致，包括发展造成的落差、经济层面的不满、移民问题、境外影响和“羊群效应”等。官方宣传的反向动员和镇压反弹更是起了推波助澜之效。然而这次事件的结果，却是让藏人普遍有了西藏独立的意识，形成广泛认同。因此，等到下一次再发生类似事件，西藏独立将会成为众多藏人的共同诉求，成为自觉的运动，并会在发展过程中作为主要的驱动力和思想指导。<br />
