21.行为艺术的合理存在之理由，便是艺术已经发展到了媒材大爆炸的一个商业化时代了，处于这样一个时代之中，那些渴望艺术不依附于媒材独立存在，不可能被商品 价值定义化的艺术家们，面对艺术受众，以生命个体独一无二身体作为媒材，把艺术从美术馆、博物馆、报章杂志那里解放出来的一种新艺术形式。<br />
 <br />
22.你有权做你想做的任何事情，只要不去践踏或侵害他人的自由。RT@zhangyumofashu 额......我要不要晒晒偶的裸体行为艺术呢？<br />
 <br />
23.身体，作为一种天然媒材，被纳入艺术领域之中，既是自由精神的一种降临，也是身体的主人不愿被权力所编码、结构、规训、型塑、改造，以及被主流社会的审美霸权意识控制的表达，更是一种用最直接的方式抵达艺术受众灵魂深处的全新艺术形式。<br />
 <br />
24.无论传统的绘画、雕塑，还是新兴的装置、影像等艺术表达样式，都在这个充满了酸腐气息的时代中，被商品与货币所定义了。艺术品收藏，也成为商人们获利最直接且最神奇的一个赚钱的市场了。<br />
 <br />
25.当艺术品变为财富快速增殖的商品之后，艺术独立存在的价值便发生了危机。然而，独立的身体，却具有不可被商品化的性质，而偶发性的艺术作品也不可能被艺术收藏者所收购。每一次的行为表演，只存在于已发生的时间过程与受众的记忆之中，行为艺术的革命性便产生了。<br />
 <br />
26.倘若说艺术的核心价值是艺术家用作品去召唤自由精神降临人生的话，那么，行为艺术，尤其是裸体行为艺术，便是一种最为彻底的自由艺术表现形式，这种新式往往是以令喜欢让人处于不自由状态的主流社会霸权不悦或尴尬的面貌出现的自由艺术；所以，行为艺术最接近艺术的本源。<br />
 <br />
27.结构与解构，自由与专制，修饰与揭露，编码与反编码，惩罚与反抗，规训与犯规训；遮蔽与去遮蔽，定义与反定义，这些通往弥赛亚之光降临人类社会的大道上产 生的永恒二元对立，都可以用行为艺术/身体艺术来获得表述。并非所有的行为都是艺术，唯独具有艺术符号能指属性的且有意识所为的行为才是行为艺术。<br />
 <br />
28. 虎年第三天，在京城一隅，一群心甘情愿把自己放逐于主流社会之外的男人，以群裸的方式，举行了一次异样的新年团拜活动。据说他们中多为诗人。在这个非常现实主义与非常物质化的时代，虽然诗歌已日趋死亡，诗人也日益被艺术边缘化，但是，诗人们，却以赤裸的身体集体书写了一次无字的诗歌，它的主题便是对自由的向往与对权力的反叛。<br />
 <br />
29. 进入文明时代后，我们的身体早已被异化，而权力，则是身体被异化的制造者。我们既然做不到时时刻刻都 赤身裸体地面对裸生活每一个场景，但是，我们却可以偶尔性来一次裸体的行为。不为别的，仅仅只为证明自己的身体与灵魂具有独立于权力结构之外，并有权与主流社会的价值体系保持间距的正当性而已。<br />
<br />
30.退回到裸体状态的身体，是一种放下，是一种摆脱，也是一种解放。放下，意味着对所有生命不能承受之重的压迫之反动；摆脱，则是对一切貌似严肃之意义虚构的解构；而解放，便是身体的主人从权力结构的牢笼之中逃离出来，给生命之存在予以自我命名。<br />
