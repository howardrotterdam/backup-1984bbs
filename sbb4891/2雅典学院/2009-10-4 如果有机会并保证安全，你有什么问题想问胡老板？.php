49．你们的经济建设，就是拆了建、建了拆。用这种办法堆砌出来的GDP，可以拿去跟人家比增长率，但仅此一点，仍不足以使你们对这种狗熊掰棒子式的经济建设如此热衷。其中还有更大的奥秘：每浪费一亿元的投资，就可以把其中某个比例的部分装进自己腰包。你们的经济建设其实不是经济建设，而是再分配的一种手段。以建设、投资之名，行中饱私囊之实。以经济建设为己任的人们，在内心里诚实地坦白一下，我说的话说没说到点子上？我还要说的就是，中饱私囊也得讲效率，你们现在的办法成本太高了：为了把一元装进腰包，就要浪费掉六、七元，外加土地、资源、环境和道德的巨大代价。你们不是提倡实事求是吗，在这个问题上能不能来一点实事求是，想拿多少钱，径直拿走就行了，不要再耍那些劳什子的投资、建设、发展的花招，把那六七倍的浪费节约下来，岂不更好？<br />
<br />
50．你们高明的统治权术里有这么一项：高喊&quot;依法治国&quot;；既然依法治国，那就得按法律办事，然后从国际上找来一套&quot;法无明文规定不得定罪&quot;的法律制度（由陪审员现场定罪的英美法系是万万不能采用的）。遇有党政官员犯了罪想包庇，可有说的了：谁让我们的法制还不健全呢，他们的犯罪行为是与时俱进的，法律条文里还没有写进这样的条款啊，法无明文规定不能定他的罪呀。对于这种依法治国和放纵罪犯的高妙统一，先让你自我欣赏去，现在只是这么问你：虽然你的法律条文总是不够用，但是你毕竟已经写出了许许多多的法律条文；这些法律条文里，有许多是关于国家工作人员能做什么、不能做什么的。这些关于国家工作人员能做什么、不能做什么的条文，绝大多数又没有同时规定他们如果不遵守这些条文怎么办。这是不是为了允许他们可以不遵守这些条文预先设下的伏笔啊？比如，你们的新律师法规定，律师有权会见犯罪嫌疑人并不被监听，却没说阻止律师会见或监听律师会见的人要受到什么处理，照你们的执法原则，当然就是不处理了。是不是这样啊？<br />
<br />
51．所谓政府出钱购买行业协会、中介机构的服务，是不是向领导亲属开办的皮包公司进行的财政转移支付？<br />
