													<h2>这是毛泽东的矫诏吗？</h2>
						
						
																											<div id="postmessage_362905" class="t_msgfont">这是毛泽东的矫诏吗<br />
从 李承鹏 作者：李承鹏<br />
<br />
<br />
毛泽东论拆迁 在中国共产党第八届中央委员会第二次全体会议上的讲话 　　（一九五六年十一月十五日）<br />
<br />
早几年，在河南省一个地方要修飞机场，事先不给农民安排好，没有说清道理，就强迫人家搬家。那个庄的农民说，你拿根长棍子去拨树上雀儿的巢，把它搞下来，雀儿也要叫几声。邓小平你也有一个巢，我把你的巢搞烂了，你要不要叫几声？于是乎那个地方的群众布置了三道防线：第一道是小孩子，第二道是妇女，第三道是男的青壮年。到那里去测量的人都被赶走了，结果农民还是胜利了。<br />
<br />
 <br />
<br />
后来，向农民好好说清楚，给他们作了安排，他们的家还是搬了，飞机场还是修了。这样的事情不少。现在，有这样一些人，好象得了天下，就高枕无忧，可以横行霸道了。这样的人，群众反对他，打石头，打锄头，我看是该当，我最欢迎。而且有些时候，只有打才能解决问题。<br />
<br />
 <br />
<br />
共产党是要得到教训的。学生上街，工人上街，凡是有那样的事情，同志们要看作好事。成都有一百多学生要到北京请愿，一个列车上的学生在四川省广元车站就被阻止了，另外一个列车上的学生到了洛阳，没有能到北京来。我的意见，周总理的意见，是应当放到北京来，到有关部门去拜访。要允许工人罢工，允许群众示威。游行示威在宪法上是有根据的。以后修改宪法，我主张加一个罢工自由，要允许工人罢工。这样，有利于解决国家、厂长同群众的矛盾。<br />
<br />
 <br />
<br />
无非是矛盾。世界充满着矛盾。民主革命解决了同帝国主义、封建主义、官僚资本主义这一套矛盾。现在，在所有制方面同民族资本主义和小生产的矛盾也基本上解决了，别的方面的矛盾又突出出来了，新的矛盾又发生了。县委以上的干部有几十万，国家的命运就掌握在他们手里。如果不搞好，脱离群众，不是艰苦奋斗，那末，工人、农民、学生就有理由不赞成他们。我们一定要警惕，不要滋长官僚主义作风，不要形成一个脱离人民的贵族阶层。谁犯了官僚主义，不去解决群众的问题，骂群众，压群众，总是不改，群众就有理由把他革掉。我说革掉很好，应当革掉。　　（《毛泽东选集》第五卷，人民出版社１９７７年４月第１版，第313--329页<br />
<br />
 <br />
<br />
读后感：<br />
<br />
是的，你现在的感受和刚才的我一样，不相信这是伟大领袖毛泽东曾说过的。我阅速一向过快，当看到这是4月第1版时，还以为这是4月1日版，加之内容悚异，就以为是网友恶搞。专门请成都理工和河南大学的同学们帮忙在图书馆查证，证实确为毛选第五卷里的话。考虑到毛是个诗人，说话常有些情绪化，但是专门收录在毛选是要很慎重的，所以我觉得，这是毛泽东的本意。<br />
<br />
 <br />
<br />
选载毛泽东的著作是不会被和谐的，对伟大领袖的思想进行学习更不应该被驴霸，所以读后感如下，望有关部门给我一些宽容，我写字为生，无以为命，爱家爱国，不想自焚，现心得如下：<br />
<br />
 <br />
<br />
一、很感动毛泽东用巢里的雀儿来形容中国人民，其实中国人民很卑微，他们大多不会关心政治，只关心粮食和蔬菜、房子、生产工具。从世界范围里，中国人是最好管理和忽悠的（这有赖于儒家思想的伟大教化），只要给口饭给片瓦，基本一辈子不会反。但不要触碰他们的底线，即，不要砸他们的锅，不要揭他们的瓦，底层生物（原谅我说出真相）在生存底线遭受挑战时，会迸发出比高等生物更可怕的战斗力，会以伤害自己及对手的方式解决问题，比如自焚。<br />
<br />
 <br />
<br />
自焚当然不划算，但这时候我们还说什么这人是否理智，是否违章，当这人已用生命来捍卫一件东西的时候，作为统治者，应该尊重他们的情感。自大禹始，我们之所以要成立一个国家一个政府，是因为要让大家活得更有安全感，更像一个人，而不是更憋屈，更他妈像一条狗，而且还是丧家的。<br />
<br />
 <br />
<br />
其实人民是爱自己的国家的，一定要相信这一点，不要疑神疑鬼，处处皆暴民，你用暴力的眼光横扫天下，天下皆暴民了。看，他们连选举权都没有，就承认你们是伟大领袖和英明管理者，还纳税，多顺，多傻的人民呵，善待他们，大家互相给一条路，借过，此为和谐。<br />
<br />
 <br />
<br />
二、建国大业之后，有些人掌了权，就不爱人民了，就横行霸道了，更由于某些地方官员拥有“不解释权”，人民无处说理，有理讲不清，就只有像祖先猴子那样扔石头还击来犯之敌，世界文明进程都这样了，我们还有这种返祖现象，您不觉得人民其实不是可恶而是可怜么。所以毛泽东说“打才能解决问题”。<br />
<br />
 <br />
<br />
很怕，在建国大业之后，再拍一次《建国大业》。<br />
<br />
 <br />
<br />
三、毛泽东居然主张修改宪法，放行进京，罢工自由，这有利于解决国家、厂长和群众的矛盾。他要是活到现在，会气得脑血栓呗儿一声就死过去。因为，现在信访群众会被当成神经病还有医院证明，到北京，暂住证也会被条子撕了，现在的老板巴不得你罢工，正找茬儿开你，就送刀口上来了。我们的《劳动法》其实接近于废纸了，《物权法》可以叫作《无权法》，倒过来念也行，它根本就是一个屁，抵不过政府一个手谕。<br />
<br />
 <br />
<br />
四、毛泽东说谁不解决群众的问题，骂群众压群众，就得革掉，革掉好。这显示出毛的幼稚，。现在的情况：有关部门可以用违法的手段来伤害人民，出了事就算是 “工作手段不合理”，行政处分了事，比如上海闵行钓鱼事件二个政府官员（相对而言成都对钟昌林的处理还算及时和深入的）；而奋起反击的人民则叫做“暴力抗法”，得拘留和判刑，比如成都唐福珍和上海潘蓉。<br />
<br />
 <br />
<br />
这就太好玩了，在一场械斗中，代表官府的是工作手段不合理，代表人民的却是违法。哪怕捉奸，你也不能说一方是调情，另一方却是强暴。<br />
<br />
 <br />
<br />
人民拿什么来革掉呢，到后了，只能革掉自己了。最悲哀的是，革掉自己后，家人还要被判处行政拘留，自大秦以来，复见连坐。<br />
<br />
&nbsp; &nbsp;<br />
<br />
五、很多地方的拆迁都是以修建公共设施作为名义的，以此假装大义凛然。但以大多数人的名义来剥夺少数人的利益，是猥琐的，最终也会伤害到大多数人利益，有关部门一向是擅于挑起群众斗群众的，让人民和人民先行火并起来，比如单双号限行，说是照顾更多数的自行车族，后来大家发现自行车也快实名制了；比如涨油价，说是保护环境、不让无车族负担过多的税钱，后来发现连天然气和自来水也涨价了；再比如为了照顾农民兄弟就粮油肉菜涨价，可农民兄弟一转眼就发现自家连地，都被任志强他们收走了……<br />
<br />
