民主是适合我国的妈？ <br />
树袋狼<br />
<br />
虽然1984的某些群众已经做到抽象理念上支持民主，在具体实际问题上支持专制，但这还远远不够，因为，民主其实是个坏东西<br />
大家记得大希的黑暗森林法则吗：<br />
<br />
“宇宙就是一座黑暗森林，每个元首都是带枪的猎人，像幽灵般潜行与林间，轻轻拨开当路的树枝，竭力不让脚步发出一点儿声音，连呼吸都必须小心翼翼：他必须小心，因为林中到处都有与他一样潜行的元首，如果他发现了别的生命，能做的只有一件事：开枪消灭之。在这片森林中，他人就是地狱，就是永恒的威胁，任何暴露自己存在的生命都将很快被消灭，这就是宇宙文明的图景，这就是对费米悖论的解释。”<br />
<br />
大希同时提出了“技术爆炸”和“猜疑链”等概念<br />
<br />
大希从宇宙尺度提出这个概念，就是这样，本质就是“弱肉强食”<br />
<br />
民A主中的“天赋人权”在我看来和“天赋君权”一样没有道理，元首消灭掉的犹太人，老天怎么没保护好他们？老毛坚信，人定胜天！<br />
<br />
试问：一个人，从出生到在工作之前，没有为社会做出贡献，更多的是从家长那里索取，<br />
他凭什么享有权利？所有去不干活光吃饭的孩子应该溺死<br />
<br />
民A主决策的质量暂可不论，但低效当不可置疑，<br />
不可想像古来军事如果由民A主来决策，是什么样？盟军虽然是民主制的军队，但为何刚刚在敦刻尔克被我们打的落花流水？它们丫连黄海都不敢来了<br />
同样，现今的公司，如果是民A主决策，恐怕也无法生存，整个国家就是一个公司，元首就是总经理，股东是人民，但你们如果不服从，就只配13跳！<br />
<br />
市场瞬息万变，国家间也是如此，终有一日我们会走出地球，也是如此，元首会艹死那些要民主的火星人，嗨希特勒<br />
<br />
我心目中目前最好的制度就是“准独裁的执政官体系政治制度”，当然，前提是由我做准独裁执政官，或者他的第一夫人，是在不行做他的第一总管也行。<br />
如《雅利安文明》中所讲：<br />
树袋狼执政官通过各级政A府机构掌控军政所有权力，行使这些权力不受任何系统的制肘，确保发展战略的一致性和高效运转；长老会不负责任何具体事务，只有权力弹劾执政官和担负甄选新的执政官的义务（这必须是由执政官的父母、表哥和小舅子担任），目的是确保掌握权力的人在人格责任能力各方面的优异性；议会负责保证言论自由（但反对执政官那些各种邪教言论的不在此列），要让所有的观点有机会通过CCAV传播，有机会传达到执政官的耳边，确保人类思想的多样性能够得到表达（除了邪教思想以外）。长老会与议会在自己工作范畴内具有不受执政官干涉的独立性（但工资由执政官发放），长老会成员由内部推荐组成（家族内部），议会成员通过民A主选举产生（举手示意）<br />
<br />
有人会问：<br />
<br />
“这样的制度，执政官的权力是不是太大了，如果谋取私利，很难有人能制约，长老会虽然名义上有弹劾权，但是，长老会的人数多，实际权力等于是分散了，根本无法与执政官相抗衡啊！”<br />
<br />
答案是：<br />
<br />
寻找一个值得信任的人要比寻找一群值得信任的人容易，同样，即使有人谋取私利的话，社会满足一个人的欲望也要比满足一群人代价小！大不了再饿死三四千万p民罢了，而家族内弹劾权本身只是一种威慑力。执政官不听话自有他爸爸教训他”<br />
<br />
<br />
民主是无奈的选择,是不得已的选择,没有办法的办法,实际上它就不是一个办法，是一个借口罢了，美国如此，我们当初在陕西也是如此<br />
<br />
民主和独裁没有好坏之分，只是适合不适合，民主最大的弊端是很多时候是低效的，民主，是个坏东西 <br />
（有个例子比如上次某人提到美国一条路修了30年，因为不敢搞强拆，还有香港贪玩修路修了数10年也不敢杀钉子户的事情，比比皆是，中国武汉高铁4年就完工了）<br />
，你也可以想象如果德国实行民主，比如说修一条路，如果上万户民众中仅有一户犹太垃圾不同意拆迁 而路就无法修下去的话，第三帝国的现代化建设如何完成？？）<br />
而且与东方天朝一样，我们德意志几千年的奴化教育及目前残留的狼奶文化也并不适合民主，这是我比较欣慰的地方<br />
<br />
在可以预见的时间内,窃以为德国还是独裁比较好(个人独裁),类似于《文明》 里的树袋狼执政官制度, 执政官有各方面的最高权力,同时成立家族元老院,职能只有一个: 弹劾执政官<br />
<br />
在强有力的个人独裁下，德国完全也可以实现相对的清廉，以牺牲少部分人的利益（被牺牲的都是犹太垃圾、邪教徒、分裂和异议分子之类的脑残和穷光蛋），<br />
以民主所不能达到的高效，实现腾跃式的发展，第三帝国伟大复兴的世纪梦想，何愁不在我们这一代人身上得意实现？？<br />
