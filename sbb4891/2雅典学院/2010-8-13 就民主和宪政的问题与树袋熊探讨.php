													<h2>就民主和宪政的问题与树袋熊探讨</h2>
						
						
																											<div id="postmessage_781294" class="t_msgfont">首先，澄清一下民主与宪政的概念，纯粹意义上讲，民主解决的是权力来源，而宪政解决的是权力运行的机制。有意思的是，民主在很多时候慢慢变成了一个包含宪政概念的混合物，使得“民主”成为政治领域里面集一切优点于一身的神器。如果这是一个现实的神器倒也不错，可惜的是，现实中不存在完美之物，“民主”神器自然也就仅仅是个概念。被完美概念忽悠的后果我们都经历过，希望这次不要重蹈覆辙。<br />
<br />
其次，民主不完美，怎么办？回答很简单，“民主是最不坏的办法”。这个回答足够有力了。人是有缺陷的，人类社会也是一样。能够找到最不坏的东西，已经是我们的终极目标了。<br />
<br />
第三，上面一条仅仅回答了权力来源民众，而权力如何运行？这个需要宪政来解决，也就是对公权力要有规则来制衡。其中包括两个含义，第一，公权力必须被不同部门分割，相互制衡；第二，要通过规则来制衡，而不是道义、领袖魅力等等其他东西。只要有这两条，公权力的结构会逐渐走向合理。反之，违反其中一条，任何看似美好的蓝图都有可能瞬间变为弥天大祸。<br />
<br />
第四，宪政和民主是有内在联系的，可以说“民主是宪政的终极价值支撑点，宪政是实现民主的最好途径”。宪政就是对公权力的有效管控，为什么要管控公权力？这是因为公权力的价值基础是每个公民的基本权利。承认每个公民的基本权利，就是民主。以这个为出发点，会推演出公权力必须被管控，而宪政是最好形式。“宪政是实现民主的最好途径”不是理论推演出来的，而是实践出来的经验，凡是以民主的名义打破一切规则（包括宪政）的实践，最后都以极端专制收场。<br />
<br />
第五，宪政和民主在短期内是冲突的，一个社会里面知识、财富、地位、权力是金字塔结构的，越到底层人数越多。宪政的运行是规则，规则的演变是个漫长而细致的过程。在这个过程中，民意（包括多数的的短期利益）都不能超越现有规则。这是解决冲突的最佳方式。<br />
<br />
第六，无论那种机制，无论民主还是独裁，除非采用抽签，实质上都是一种精英治国，这个并不需要讳言。随着人类社会群体数量的膨胀，社会分工的细化，真正运行公权力的人是非常少的，而夺取公权力的话语权已经成为了一门高深的学问。这种学问并不是坏事，只要给他足够的限制，让它足够透明就可以了。人类所有的进步都是领域专业化发展的结果，政治实践领域要反其道而行之显然是可笑的。<br />
<br />
第七，以宪政的名义排斥民主是荒谬的。说了大半天，一再强调宪政高于民主，优于民主。是不是宪政就可以否定民主，忽视民主，排斥民主？参见第四条，这种理念是极其荒谬的。好的宪政是以民主为基础的，是以民主为目标的，是受到民主制约的，这是都是检验好宪政与坏宪政的标尺。以国情的名义让民主短期内屈从宪政可以接受， 但是必须同时强调民主的终极性，而且必须给出践行民主的路线图、阶段性目标和时间表，这就是好的宪政。反之，否定民主的终极性，或者无限期、无计划、无目的的拖延，也都是坏的宪政，由于他的基础是反人类的，必定会被历史潮流冲垮。<br />
<br />
第八，现在中国的问题是，既没有宪政，也不民主。公权力没有制约，民众也没有能够行使政治权利的途径。如前所述，最理想的路径是先建立宪政的机制，在此机制下逐步提高民主水平。这个需要执政党必须有足够的政治智慧和决断。他们首先要实现真正的宪政，也就是真正的分权制衡（哪怕在执政党内部形成也可以），其次，要实现好的宪政，也就是有目的、有阶段、有时间表来扩大民主程度。这样才有可能以最小的代价引领我们走向真正的政治文明。如果执政党无法实现这些基本设想，那么社会矛盾的激化只能是堰塞湖水位的提高，各种维稳的手段不过就是让民间的破坏力量逐步积蓄，等待最后崩溃时刻的到来。而这种破坏是很纯粹的，人们只有在破坏后再逐步吸取教训，经过多次动荡才能重构社会的价值体系和规则。<br />
<br />
中国的未来道路如何，其实就看执政党的政治智慧和民众的忍耐力。其他什么公共知识分子，国外颠覆势力，国内有组织有目的的反政府活动能够起的作用都极其有限。真正具有摧毁力量的是民众，他们只会在能够忍耐和不能忍耐中间选择，这个临界点之前，他们基本上不会受到各种思想的影响，而且各种思想、组织本身就混战不已。在这个临界点之后，政府所有的权威和体系将被彻底冲垮。</div>

							
							
							
							
