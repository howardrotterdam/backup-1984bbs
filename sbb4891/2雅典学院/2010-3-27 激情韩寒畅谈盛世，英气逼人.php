																											<div id="postmessage_535646" class="t_msgfont">今年二十八岁的韩寒，在十天前的拉力赛不幸遇翻车事故退出比赛后，写下了“这就是先不感谢父母下场”的博客，受到了无数追棒。最近在接受外媒专访时，展示了真正中国青年的风采，令人无不动容，俺甚至第一次被他深深地打动了，他的思想早已超越了20年前曾经义气风发的一代大学生，在今天的盛世中国还有这样一个激情与理性同在小伙子当世界第一博客大侠，真的让人感到非常幸运。<br />
<br />
&nbsp; &nbsp; 在谈到上海时，他说：“你想上海这个地方，有什么可以给人家开眼界的吗？有什么作家吗？除了余秋雨，结果余秋雨还是被大家唾弃的。上海没有什么拿的出手的东西，电影导演没有，作家没有，艺术家也没有，基本上都没有，就这么一个城市，拿钱堆一堆，我也能堆啊！<br />
<br />
&nbsp; &nbsp; 拿钱堆出一个城市，很简单，然后房价搞那么高。如果我是上海市=长，我会很不好意思，请大家先别来，我先搞一搞再来。我觉得这个城市对现在的中国来说，是一个耻辱……现今的上海，其实没有什么值得炫耀的地方。<br />
<br />
&nbsp; &nbsp; 上海世博会，肯定有人喜欢，有人不喜欢，有人喜欢清静，有人觉得挺好的，任何一个国家都会有这么两批人，但在上海就只有一批人，就是大家要欢欣鼓舞地来搞世博会。但没有另外一批人吗？完全有，只是那批人完全都被“和-谐掉了”，这样一个城市是没有资格成为一个国际大都市的。你要看一个城市是不是伟大，不是看他的楼有多高，中国人不要觉得自己有多厉害，还有中东人啊！他们用石油赚来的钱，可以搞的更野。<br />
<br />
&nbsp; &nbsp; 其实中国的城市发展是很野路子的，就是很多外国人不敢用的东西，中国人都敢用，包括车子也是，像奥迪答6，外国年轻人都不太开，因为觉得太前卫了，在中国居然是当官的人在开。上海是一个冒险家的乐园，但冒险家的乐园百分之百就是人-民的地狱。<br />
<br />
&nbsp; &nbsp; 但是冒险家是不存在有乐园的，因为冒险家有成功，有失败，如果你能确保这些人百分之百都是成功的，那说明很多本来属于人民的钱，都用于投资回报了，会让上海的人民非常的痛苦。当然那种痛苦，可能都是私底下的痛苦，镜头对着他们，突然就说上海很好。这不能说是好面子，而是一种自尊心，就是自己可以说它不好，但对外面的人不行。”<br />
<br />
&nbsp; &nbsp; 看到这些话时，想到在上海的姐姐，心里真的为之一颤，怪不得她告诉俺抓紧在北京出头，抓紧赚钱，用人民币武装自己才可以进入真正的上流社会，潇潇洒洒走一回。然后，就对俺说了一大堆牢骚，当然没有小韩总结得这般到位。<br />
<br />
&nbsp; &nbsp; 不过，看上面的这段话，其实对于俺这样年纪的曾经同样激情豪迈的一代人来说，并没觉得深刻，只是共鸣。而当我看到小韩后面谈到的内容，真的第一次为这样的80后叫好、欢呼！因为他不光激情，而且完全成熟可以经受任何风浪了――<br />
<br />
&nbsp; &nbsp;“真实中国的状况，我很难用几句话来形容，这个问题问的很大，我觉得整个中国都被所谓的国内生产总值压的很沉重，因为我们特别大，好面子，在金融危机时都要保持一定的经济增长率，在这样的政-府之下，完全可以这么做，可以把房价弄得很不可思议，完全可以保证国内生产总值的增长率，但却给大家的生活带来特别大的压力。<br />
<br />
&nbsp; &nbsp; 只有少部分人通过官-商勾结，或是真的靠自己本事做出来，获得了非常巨额的财富，可以让外国人感到震惊的财富，可以去洗劫路易威登（LV）号店，但这绝对不能代表真正的中国市民的真正生活情况，这还仅仅是大城市的人民，更不用去提农民工或是居住在偏远地区的人民。<br />
<br />
&nbsp; &nbsp; 他们把房价弄这么高，但中国人很贱，非得买，因为结婚时，女方就会要求你有房子，男方倾家荡产也得买房子，买了房子之后就必须还月供（付房贷），最后导致了你不能失业，你一失业三，四个月之后，月供就还不上了，所以就没种。就会拿自己的人-权和尊严去交换。如果有一批人不在乎，那很好，可是在中国，尤其是大城市，大家都很在乎工作，所以只能在网路上泄愤。<br />
<br />
&nbsp; &nbsp; 我只是个充气娃娃而已，我只能让大家爽一下，我不奢望能改变什么，或是有人看了我的文章，就对老板拍桌子说我不干了，一方面我害了他，一方面如果你没有能力，中国的社会保障不好，还是干着吧。中国的发展取决于最高的利益阶层，我不把他们叫做最高“权力阶层”或“领导阶层”，而是利益阶层，取决于他们的方向。<br />
<br />
&nbsp; &nbsp; 我们现在看到很多人不满，但是他们的不满，不是说要推=翻乡镇政府，然后自己搞一个乡=镇政府，可能是因为他们答应要给你十块，结果只给五块，引起老百姓不满，只要你给他们剩下的五块钱，老百姓就欢天喜地的回去了。所以，今天的中国人不是要保护自己的“权=力”，而是自己的“权益”，他门现在还只是在这个阶段，你只要保障了他们的权益，他门就不会去想别的。<br />
<br />
&nbsp; &nbsp; 他们反贪-官，不反皇=帝。他们总是认为上面的政策是对的，只是下面的贪官弄歪了，现在还是这样的观念。我之所以没有这么做，是因为我觉得不是每个人都像我这样衣食无忧。看到自己想反对的东西，现在在中国的情况是，第一，去抗-议，游-行了也没用。第二，绝对无法形成规模，现在怕的人比不怕的人多，在这种社会下，你要做的就是写更多的文章，让更多文化的东西去改变他。<br />
<br />
&nbsp; &nbsp; 所以，我们的领导应该感到很幸福，因为你有这么一群，说难听一点，这么蠢的国民，说好听一点，就是有一批这么忠诚，这么相信你的国民，哪怕自己受到欺负，他们还始终相信你。所以我觉得应该多分他们一点东西，让房地产价格不要这么高，经济增长缓一点，给他们更好的福利吧。<br />
<br />
&nbsp; &nbsp; 我现在做了那么多事情，就是想让更多外面的人在看中国的文化时，不是带者一种鄙视，不屑的眼光，而是知道中国也有很多人希望有更好的空间，有能力写出更好的东西。是有这样的人存在的，你只要给他更好的空间几可以了。再过一段时间，社会更成熟，更有文化，大家就不再是利益集团可以买得起的了。”<br />
<br />
&nbsp; &nbsp; 好爽的韩寒！世博会俺一定力争和你在上海相见。媒体生涯这么多年，让俺冲动的年轻同行不多，而你恰恰才是一个典型的中国“声音”，太爽啦！<br />
<br />
&nbsp; &nbsp; ……<br />
<br />
&nbsp; &nbsp; 第一次心动的转载了韩寒的访谈，爽过之后面对的依然是大旱的画面和主流发出的“胜利”的消息，什么用水问题解决了、解放-军负责灾民用水、灾区菜价不影响 CPI上涨、明星组织向灾区捐款、村民存粮能维持两个月、泼水节因旱灾减少水量缩短时间等等，一切灾情似乎都是昨天发生的事，今天就又阳光明媚了。<br />
<br />
