																											<div id="postmessage_248804" class="t_msgfont">我初中时（2001年左右）就知道64事件了，虽然不了解具体情况，但从那时起我就成为一个坚定的“反革命份子”。<br />
<br />
最初影响我，使我开始独立思考的可能是“9・11”事件，巨大的伤亡人数使我从前年的盲目跟风抗议美国误炸TG大使馆的反美情节中走出来，我开始冷静下来，而那时学校里面的小屁孩兴奋的幻想美国亡国，TG称霸世界。我完全没有知己。<br />
<br />
高中，我接触到胡适，我读了大量他的著作。真是感谢他老人家，我模糊的思想彻底坚定。不过，对我影响最大的还是李敖，虽然大家现在不待见他鸟。最厉害的时候，我一连十几天几乎把《李敖大全集》K完。这时我对很多事情的看法就逐渐透彻。<br />
<br />
我所在的高中号称省重点，全封闭管理，平时出门要请假条。我呢，经常伪造老师的签名，自制一些假条。当然不能用真名了，我自称“李放”。这导致，我跟门卫熟识后，他跟我一口一个“小李啊”“李放呀”……<br />
<br />
出校后，去上网，我也不玩游戏，只是随便在摆渡里搜“八九”啊、“64”啊、“魏京生”啊，那时根本不知道什么GFW。偶然的机会，在一个叫维基百科的地方，我看到了“64事件”的条目（那时维基刚起步，条目还不完善），我当场一字一句抄下来。终于知道完整情况鸟，兴奋、激动，我像中了500万一样。经常拿出来翻看。<br />
<br />
偶尔，一些成绩差的天生反骨的同学会跑来和我讨论他们的困境，不过，当我试图将话题引向独裁专制民主自由时，他们会表现得索然无味，谈不下去了。那些所谓成绩好的家伙，只关心方程式啊、什么酸碱、什么物理定律。我几乎没有一个可真心交流的对象。<br />
<br />
当我那本手抄本快被翻烂的时候，父上大人半推半就，勉强给我买了台电脑。真系猿粪，没几天我就下载到了那部《天暗门》，三个小时，我一把泪涕。<br />
<br />
大学鸟，宿舍的几个家伙，整日嘀咕女人啊逼啊爽啊，我几乎都没搭理过他们。而接触的其他人，也市侩得一塌糊涂。我索寞得不行，只好钻到图书馆里，看那些内部出版的发黄旧书。<br />
<br />
像我这样的人太少，现在我真是度日如年。我也累了，对这个国家不抱幻想。为贵国人民奋斗?完全不值得。降生在这里，并非我愿，没什么值得自豪的。我要努力赚钱，尽早争取米帝绿卡。</div>

							
							
							
							
