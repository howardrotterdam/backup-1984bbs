													<h2>陶杰：一念天堂一念地狱</h2>
						
						
																											<div id="postmessage_286496" class="t_msgfont">「赵紫阳先生是一个好人，但不是一个成功的中国政治家。」<br />
　　--陶杰<br />
　　　　<br />
一念天堂，一念地狱，在一九八九年六月四日前後，中国的赵紫阳先生在一念间选择了天堂。<br />
　　<br />
在现实残酷的中国政治，赵紫阳或许根本不是一个适合「从政」的人。<br />
<br />
中国政治的成功者，从三国的曹操丶明代的朱元璋丶清末和民国的袁世凯，都不靠道德的力量坐上胜利者的宝座。中国的政治永远只有「成败」，没有对错。赵紫阳先生是一个好人，但不是一个成功的中国政治家。在宣布戒严令之後，他审时度势，一定知道自己处境孤立：虽然是所谓总书记，但赵紫阳上有真正的大老板邓小平，还有一个「中央顾问委员会」的老人董事局，旁有保守的李鹏和姚依林，下有在天安门广场绝食的大学生。杨尚昆丶乔石丶田纪云丶胡启立，内心不无对学生同情，对军事镇压大表保留，但个个都望风蛰伏，噤若寒蝉，他们都知道中国宗法家长制的潜规则。<br />
<br />
赵紫阳的失策，是没有在学生运动之初，与学生建立秘密的沟通渠道，让学生明白：「中央」有两派，示威静坐到甚麽时候，听「总书记」的讯号。等到将来老头子统统百年之後，甚麽民主改革，到时有希望，但绝对不是现在，请大家明白总书记的处境。<br />
<br />
但大学生不懂得阅读中南海的权术气象，断送赵紫阳政治生命，很残酷地，是群龙无首丶理想主义的大学生。及时撤退是唯一的政治抉择，但大学生选择了浪漫。<br />
　　<br />
这时，轮到赵紫阳来抉择了：如果他想做一个中国政治家，他必须学周恩来，在文革之中，明知毛泽东决不放过刘少奇，周恩来亲自执行整肃刘少奇，宣布刘少奇是「叛徒丶内奸丶工贼」的党内决议，牺牲刘少奇一人，保全其他「老同志」，最终也保住自己。赵紫阳应该跟随杨尚昆，政治局常委投票，支持军事镇压，先熬过这一关，赢得邓小平信任。经历了「考验」，赵紫阳最终会掌继军权，再等十年，老人董事局凋零崩折，赵紫阳权独天下，这才是实践改革理想之际，才是大展鸿图之时。<br />
<br />
在残酷的中国宫廷，任何拥有独立人格的人物，都要潜藏养晦，等待时机。赵紫阳经历过文革，他一定知道林彪的戒条：「悠悠万事，唯此为大，克己复礼」，自勉要压抑人格，遵循礼制，才办得成大事；江青的自励诗：「江上有奇，锁在雾中，寻常看不见，偶尔露峥嵘」。林彪和江青的殒灭，是他们终究做不到：一个终未能克己复礼，心浮气躁与毛泽东争权，一个走出了雾，头角峥嵘地暴露了野心。赵紫阳与在蒋经国面前「屁股坐三分一」的李登辉相比，他的道行太不足了―假设「二二八」发生在美丽岛事件的一九八○年，如果蒋经国叫李登辉来镇压，面对一群政治异见的台湾人，我相信李登辉还会第一个率先开枪。<br />
<br />
但赵紫阳太有良心了。中国的政治却从来不是良心人士的游戏。杀人之为「成者为王」的手段，实在太平常。康熙文字狱大杀知识分子，曾国藩的湖南团练剿太平军村村灭族而赢得一个「曾剃头」的绰号，但康熙和曾国藩，都是今天中国人心中的历史英雄。<br />
<br />
赵紫阳如果看远一点，等久一点，下手狠一点，天安门外的死难血腥，当然都挂一部份在他帐上，但他最终会赢得一个天下，谋取更大的英名，世人会记得他是中国的戈尔巴乔夫，手上的一点血，瑕不掩瑜，算不了甚麽。<br />
<br />
正因为他一念天堂，没有选择「我不入地狱，谁入地狱」，他一人赢得了历史的清誉，他管治的国家，丧失了珍贵的时机。他是道德的赢家，政治的输家。<br />
<br />
在中国人社会，如果想「从政」，要以赵紫阳先生为鉴，好好地想一想：你自己有没有一副厚黑的心肠，如果只有一腔慈悲，你该不该走进这座食肉的森林，而且更重要的是：这样一个奇特的国家，你救不救得了，值不值得你来救。</div>

							
							
							
							
