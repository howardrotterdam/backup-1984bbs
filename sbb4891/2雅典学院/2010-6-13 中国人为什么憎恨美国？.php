<img src="http://www.rongchang.com/admin/webEditor/UploadFile/200992113626673.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
中国有那么一批人，把子女配偶送到美国去学习定居，非法聚敛的巨额民脂民膏也转移到了美国，甚至于本人也暗中办了一份“美国绿卡”，却在国内高举反美大旗，动不动就在公众场合对美国口诛笔伐，大肆颂扬社会主义制度的优越性，大肆攻击民主、人权、法治，为“中国特色”的发展模式大唱赞歌。人们奇怪的是，那些大力宣传社会主义制度优越性的贪官政客们，为什么不把自已的亲属财产移送到“正牌社会主义国家”的朝鲜去？<br />
<br />
越来越多的贪官政客喜欢“裸体做官”，有条件者多把财产和亲属转移到了国外；眼下没有条件者也在努力创造条件。贪官政客转财移亲的首选国家不是他们满口称颂的“阶级兄弟”朝鲜，而是他们破口大骂的万恶资本主义代表美国、加拿大和澳大利亚。坏人心中也有杆称，无论他们在台上如何抨击美国、声援朝鲜，他们心底都知道美国是标准的绅士贵族，朝鲜则是赤裸裸的流氓无赖，美国才是真正文明进步最适合人类居住的国家。他们在台上大骂美国不过是忽悠平民百姓，方便自己长期公权私用以权谋私。当他们是掌握公权的官僚时，他们喜欢中国和朝鲜，因为中国是“官本位”，官主宰一切。但当他们是普通公民时，自然而然喜欢美国。美国是“民本位”，人民最大。那些主张学习美国的民主法治经验来推进中国的文明进步，因而被贪官政客污为“崇洋媚外”的人们，多数都选择留在国内；就算有条件移民美国也一样选择留在国内；就算承担相当大的政治风险也一样舍不得离开自己的祖国。<br />
<br />
中国是一个习惯说假话的民族，语言和行动常常存在极大的反差；尤其是贪官政客在台上几乎不说一句真话。在一个谎言成习的国度里，一个人的语言往往是“忽悠他人”的；行动才折射出他内心的真实想法。把亲属财产转移到美国的群体，他们内心的真实想法无疑是认同美国否定中国的。谁也不会明知对方是狼窝虎口也要把亲属财产往虎口里送，更不会选择从光明跳进黑暗里。所以那些“揣着绿卡骂美国”的群体，绝对不是真的反美仇美，而是认为自己全家到了“阳光地带”，在多数同胞面前有一种强大的心理优越感。为了长久保持这份心理优越感，就希望多数同胞的生存环境越差劲越好，最好永远生活在专制不公的黑夜，永远等不到人人平等个个幸福尊严的黎明。因此他们最害怕中国学习美国走上文明进步之路，缩小他们和多数国民之间的强大差距。裸体做官的贪官政客多属此类型。<br />
<br />
要想中国永远腐败不公，就得永远特权专制，千万不能学习美国的民主法治。因为一旦中国实行民主宪政，他们就会丧失公权私用贪污受贿的机会。一旦实行民主直选，把官吏的监督升降任免权交还到民众手中，99%以上的“裸官”都会被选下台，甚至于连过去的贪贿罪行也要受到毫不留情的责任追究。 <br />
<br />
也有一些移民美国的学者专家，因为在美国无法象当初在中国一样出人头地，就转过身来忽悠欺骗中国特权阶层，用诋毁民主、颂扬专制的谎言来从特权阶层那里换取御用，谋求在中国大陆的各种市场。这号人口口声声咒骂美国民主推崇“中国体制”，但如果要他们全家离开“万恶资本主义”美国回到伟大光荣的中国大陆，他们却无论如何也不会答应。<br />
<br />
不过，揣着美国绿卡的中国人，也有真正憎恨美国的群体；这一类型主要限于身居海外贪官政客子女。贪官政客利用职权转移到美国的子子孙孙，多数都不学无术品格低劣，除了钱外什么本事也没有。可美国主流社会是一个“认人不认钱”的国家，如果没有真才实学和必要的公德意识，绝大多数美国人就会打心眼里睢不起，就算你住着豪宅驾着豪车也一样是社会边缘人士。所以这些人尽管身在美国，却只能和其余中国同类来往，在美国结成一个“小中国”，继续用中国式人生品味和价值取向过颓废式“中国生活”，继续用“权钱”来衡量人生价值。他们被排斥在美国主流社会之外，自然会产生一种沉重的心理失落感，同时对美国滋生一种憎恨。他们不理解美国何以“认人不认钱”，何以害得他们无法显摆自己的优势。他们多么希望美国社会也象中国一样“认钱不认人”，有钱有权就高高在上。这是一个真正憎恨美国的“绿卡持有者”；但这种任憎恨美国，就象“妓女憎恨淑女”。<br />
<br />
当然，在中国大陆还有一些“憎恨”美国的普通平民，他们对美国的民主制度反感。这主要归功于专制特权集团诋毁丑化“西方民主”。因为“民主”是“特权”的克星。为了长久维护自己凌架于平民百姓之上的特权，那就得把“民主”驱赶得越远越好，最好把“民主”妖魔化。在几十年如一日的官方教育与宣传下，很多普通民众也不自觉参与了诋毁“民主”的行列。尤其是官方长期将西方“民主直选”与“贿选”、“金钱操纵”、“黑社会操控”划等号，再加上多年来中国“一半专制一半民主”的“村官直选”出现的“贿选”、“金钱操纵”、“黑社会操控”的弊端，一些普通老百姓也就认为美国的选举完全和中国的“村官直选”一摸一样，从而对美国的民主制度反感。其实西方“金钱操纵选举”的现象民主政治初级阶段偶尔出现在基层政权，但因为民主体制新闻自由，媒体记者一旦发现“贿选”现象，很快就会向全社会曝光并炒作得热火朝天，让贿选鸡飞蛋打。所以冷战后的西方成熟民主国家，基本上都消灭了“贿选”现象，“金钱操纵选举”早已成为不可思议的往事。<br />
<br />
当然，利用电视、电台、报刊、标语、车辆竞选是要花钱的，电视里那些总统候选人热火朝天的竞选场面，观众心里随便一盘算就知道要花很多钱。一些中国普通平民就会据此认为是富人给候选人提供竞选经费，把对方扶上台后来为自己的集团服务。但拿中国人最熟悉的美国总统竞选为例，就能说明事实真相。美国总统竞选需要一大笔竞选经费是众所周知的，但这笔经费的主要部分并非来自富豪的政治献金，而是来自社会捐献。社会捐献竞选资金在中国人看来不可思议，但在美国却是司空见惯的普遍现象。绝大多数美国人都有为自己认可的事业捐献金钱和劳务的习惯，并且这种捐献习惯终其一生。当某个美国人认定某个候选人最适合当总统时，就会主动为其捐献竞选基金。一个普通国民的捐献额可能是杯水车薪，但千万个“杯水车薪”加在一起就是一笔可观的竞选基金。总统候选人不能接受富豪提供大额政治献金已成为全社会的共识，而美国富人的主要捐款方向是慈善事业、学校和教会机构。<br />
<br />
为了彻底杜绝富人用金钱左右选举的悲剧，美国总统选举法规定：如果某个候选人筹集不到足够的竞选基金，国会可以提供财政援助，为其提供几亿美元的竞选经费，足够让这个人完成正常的竞选。除了“财政援助”外，还严防有钱人用金钱垄断话语权。美国总统选举法规定：如果某个候选人购买某家电视台对公众发表电视讲演，该电视台必须在不收受一分钱的前提下为此人的竞选对手在同一时段提供同等时长的演讲机会。有了上面的防范措施，富人用“金钱操纵选举”现象就会成为不可思议的往事。因此，中国大陆一些憎恨美国的普通平民，一是受到官方舆论诋毁丑化“西方民主”的影响，二是对美国缺乏基本的了解。<br />
<br />
“富人给候选人提供竞选经费，把对方扶上台后来为自己的集团服务”的猜想在美国民主制度中也完全行不通；美国各级政府的钱袋都控制在代表人民利益的各级议会手中，政府的每笔政务开支都得事先向议会申请拔款，事后要详细向议会汇报每笔钱的用法用途。为了防止政务官贿赂议会成员，行政开支还得向全社会公示，接受全社会的监督，尤其是公共工程的监督甚严。如果由政府招标修建的某座桥梁的正常市场造价为一百万美元，政府绝不可能预算出一百一十万美元来，否则就会受到舆论的质疑和法律追究。美国民主制度，也极有效地杜绝政府出现豆腐渣工程和形象工程。<br />
<br />
有网民称：中国的地方官员普遍缺乏政治信仰，腐败就是对信仰的一种抛弃，对信仰的迷惑，对信仰的不信任。政治变成谋生的手段，变成改变物质生活的源泉；信仰危机也就是国家危机。<strong>但一位在中国居住了20多年的美国官员，一针见血地指出：中国的问题，其实很简单，就是那么大约500 个特权家庭的问题。这500个家庭，加上他们的儿孙、亲友及身边工作人员，构成了约5000人的核心体系。他们之间还存在着普遍的通婚联姻的关系。他们垄断权力、形成利益集团，竭力维护现状，并制造了“一旦民主，就会天下大乱”的谎言；十几亿中国人民，都成了这个小集团的人质。中国政治深层次的改革最大的阻力就来自于他们这群人。破解他们的力量只有靠网络媒体及普通民众。</strong><br />
<br />
笔者希望中国强大，更希望中国稳定，但从不排斥说出实情的人和那些实在的真话。如果我们不能通过改革这种最小代价取得成功，而必须通过整个民族血的代价取得成功，那将是整个中国人民不堪承受之重。<br />
<br />
在现代文明快节奏下，美国作为能够保持一百年长盛不衰，能击败所有挑战对手的国家一定有其不可忽视否认的优越之处。这些优越之处说不准恰恰是我们民族所缺少的，是中国由盛转衰的根本原因。中华民族要想复兴，重新找回“天朝大国”的尊严和自豪，就得敞开胸怀学习借鉴那些明显优越于自己的文明成果，靠玩弄阿Q式的“精神胜利法”，放大“反美精神”，除了自欺欺人外，对美国一点害处也没有。 一个理性成熟的国家不但要虚怀若谷接纳吸收先进国家的文明成果，连敌人的文明成果一样要敞开胸怀学习借鉴。近邻日本就是靠“学习敌人”一步步富强文明起来的，早先向打败他们的中国敌人学习；后来向强迫他们签订不平等条约的“西方敌人”学习，最终超越了他的敌人“中国老师”和“欧洲老师”。没有哪个日本人把“学习敌人”误读成“崇洋媚外”。中国一样有向战胜自己的敌人学习的必要，尤其有向美国学习的必要。<br />
