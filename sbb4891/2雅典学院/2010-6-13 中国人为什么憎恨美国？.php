颜昌海为什么受欢迎&nbsp; &nbsp; <br />
 (2010-06-08 08:43:16)转载标签：杂谈 分类：时论酷评 信力建<br />
<br />
在凤凰博报报栏一处，有浏览最多的统计，颜昌海名列第二。网友会觉得奇怪，颜昌海一介平民为何会有如此之高的点击率呢？当你细细研究后，会觉得这并不出奇。<br />
<br />
颜昌海，湘西沅陵官庄镇人，在长沙读大学，做过数年记者，现居深圳，凤凰名博。我与颜昌海的结识仅限于博文间的交流，思想间的碰撞，往往一些观点刚想著文阐述，就发现昌海兄的妙文已经出炉，有些写得比我还好，不禁敬佩起昌海兄来。<br />
但敬佩不止于博文，我更敬佩昌海兄仗义执言、关注弱势群体的为人，每次社会不良现象出现，总有他独家的精彩解读，这种解读往往让我们看到事件的本质，颜昌海的每篇博文，我们更愿意看成是在普及常识，是公民启蒙的精品读物。<br />
<br />
而颜昌海的博文更像在践行美国实用主义哲学家皮尔斯的“不明推论式”主义，也就是说在写作和说话时，目标并不是向每个人显示自己多么正确，而是尝试促成道德风气的改变，借此如实揭露侵略，防止或放弃对于民族或个人的不公惩罚，认清权利和民主自由的树立是为了每个人的规范，而不只是为了少数人以致引人反感。<br />
<br />
来源：(<a href="http://blog.sina.com.cn/s/blog_48b0011f0100j4j1.html?retcode=0" target="_blank">http://blog.sina.com.cn/s/blog_48b0011f0100j4j1.html?retcode=0</a>) - 颜昌海为什么受欢迎_信力建_新浪博客<br />
不偏不倚、毫无邪念是对颜昌海博文的印象，我推崇的还有颜昌海的叙述方式，没有夹杂多么难以理解的专业术语，语言也不生涩拗口，读来譬如连环扣般一环接一环，欲罢不能。没有华丽的表达，但却蕴含许多真理。正如闾丘露薇评价刘瑜的文章一样：一个本可以用专业术语来展现高深的人，却选择了使用生活化的语言，这才是我心目中的知识分子。颜昌海，在我心目中亦是如此，不需精雕细琢却浑然天成。<br />
<br />
然而在中国，很多知识分子经常害怕承担罪责，在敏感问题总是逃避了事。当然这与中国的传统文化息息相关，人被奉为万物之灵，但是在现实生存中，个人的精神自由与独立追求却往往被群体精神所压抑。因为中国文化强调的核心不是个人，而是群体，个人权利的空间仅仅限于独善其身。所以，当知识分子的理念不被认同，反而频繁被套上“西方走狗”“汉奸”等等罪名时，他们选择沉默。<br />
<br />
就如萨义德在《知识分子论》一书中所说的，对于当代知识分子而言，以往客观的道德规范、合理的权威已消失，他们生活在困惑的时代，只是盲目支持自己国家的行为而忽略其罪行。知识分子不是专业人士，为了奉承、讨好极有缺憾的权力而丧失天性。而是具有另类的、更有原则立场的知识分子，使得他们事实上能对权势说真话。对权势说真话，不讨好于权势的知识分子，颜昌海当之无愧。<br />
<br />
但是，对权势说真话，除了说的必须是真话外，还应该选择正确的方式，通过最广泛的途径，影响更多人，启迪更多人，拯救更多人。颜昌海博文的高点击率以及转载率，都让我们相信人民群众的眼睛是雪亮的，文革远去几十年了，人民不再愿意被蒙蔽，他们逐渐愿意对话和沟通去表达诉求，解决问题。这些社会的明显进步，离不开颜昌海的努力和坚持，也离不开颜昌海即使被恶意攻击仍然孜孜不倦的“说教”。<br />
<br />
苟利国家生死以，岂因祸福避趋之。我想，这便是颜昌海受欢迎的最主要的原因。<br />
