													<h2>正态女：为什么应该反对国家包办福利</h2>
						
						
																											<div id="postmessage_495426" class="t_msgfont">国家与国家是总是在合作，并竞争博弈着的。美国建国后，很快就从一众资本主义国家中脱颖而出，并且多年保持超越领先，原因在于其体制，在于它的体制下的社会有更多的自由、更高的效率、更容易激励创新。 <br />
 <br />
&nbsp; &nbsp; 奥巴马上台后，想仿照欧洲实行他个人理想中的全民医疗体制，翻天覆地把美国变成一个福利国家，即国家向全民提供公费医疗。但是，民众似乎不领情，不支持这种体制，宁愿保持由市场提供、民众根据个人情况自由购买医疗保险的方式。欧洲模式和美国模式的差别就是欧洲福利国家多收税，政府包办福利；美国模式就是政府少收税，民众自己决定购买。<br />
 <br />
我们来看看现实中福利国家的面临的弊病:<br />
 <br />
福利国家有个特点，高税负。假如象其他福利国家那样实行公费医疗，美国无他法，只能提高税率。在发达国家中，美国是唯一没有实行由财政支持全民医疗的，民众税负相比福利国家低很多，经济就比福利国家活跃和繁荣。<br />
 <br />
福利国家由于都存在着国家干预市场或扭曲市场。而美国现在的医疗体制由市场经济解决，不象欧洲福利国家那样扭曲市场，市场经济下的美国的医、药等行业，有着更多的研发和创新。在福利国家，医生给病人看病，然后向政府收费。理论上付钱的才是上帝，所以政府才是上帝，不管真正的顾客--病人是否得到满意的服务，医生其实不用在意，政府也无法无处不在地监管。这种天生被扭曲的商品关系注定这种福利性服务质量不会很高。正常的情况应该像这样的：只有病人满意了才给钱，不满意下次不来了，医生才会真正在意服务质量<br />
 <br />
福利国家都存在严重的福利资源滥用：实行公费医疗，刚开始民众都觉得很满意，接着大家都想占公家资源的便宜，于是出现滥用。比如以英国为例吧，英国人口6千万，有540万壮年人口不工作，其中270万在领病残救济，162万领失业救济，80万单亲家庭受到资助，每年政府为病残救济这一项就支出120亿英镑。问题在于，据政府经济顾问David Freud估算，在270万病残救济对象中，有近200万根本就不应该享受福利，只有70万左右是真正严重的病残者，其他是“瘾君子、懒惰、没病装病之类的人”；而在加拿大，也有不少人没病装病，或以“慢性疲劳综合症”、“腰酸背疼”“失眠”为借口，让医生开个证明，去领救济或补助。公费资源的被钻空子滥用，助长好吃懒做，造成很多真正需要的人用不上。而且欧洲国家福利支出占GDP的比重越来越大，如，意大利24%，德国27%，法国29%，丹麦28，各国财政都面临不小赤字，不少国家，如英国已经开始寻找方法改革福利制度，得到在野党、媒体和民众普遍赞同。<br />
 <br />
难以忍受的低效率和较低的服务水平：由于福利资源被滥用，福利国家都面临财政负担入不敷出，为了控制债务或赤字，只好规定对福利资源进行排队供应，这就造成效率极低。以加拿大医疗体系为例，普通的感冒发烧看全科医生还好，但真有重病，等候3-4个月才能看到专科医生是很普遍的。我所知道的2例子，有个病人需要做膝关节出问题，要等6个月才能做做手术，在等候期间，只能由全科医生给她开止痛药止痛；还没有等到手术，她已经止痛药上瘾，她告诉医生这样下去不信。医生还让她不要着急，说等她做完手术后再给她安排戒毒疗程。等到她做完手术再去找医生的时候，医生告诉她，已经安排好了戒毒，等6个月就可以进去了。不过这6个月内她还要继续用止痛药。另一个例子是我认识的朋友，患了腰椎间盘突出，但约见专科医生需要3个多月，由于信息错误，第一次和医生的appointment错过，结果再约还需要3个多月，半年多等下来，朋友真是痛不欲生。这就是现实福利国家的效率。在中国人看来看个病要等几个月，真是不可思议吧，在福利国家，现实就是这样。<br />
 <br />
&nbsp; &nbsp; 再以英国的公费医疗为例。他们的公费医疗体系雇佣了了无数人，花费了无数纳税人的钱，可是平均等待一个手术的时间却大大超过更市场化的美国。医疗队伍短缺，造成每年五万个手术取消，将近四成的肿瘤患者无法得到肿瘤专家的及时诊断，而延误治疗。因为福利制度与生俱来的效率低下，滥用资源，毫无进取心，欧洲的医疗服务水平跟美国比，差很远。明显的例子，乳腺癌的女患者在美国生存率达到83.9%,而英国才69.7%；欧洲新生儿死亡率也要比美国高。于是，福利国家的有钱人或权贵有大病都跑到美国去治，比如意大利总理贝卢斯科尼，要做心脏手术也愿意大老远飞到美国来医治，加拿大前女总督乳腺癌也飞到美国去治，其中原因除了美国有更好的医疗水平，还有很重要的原因就是福利国家效率太慢。<br />
 <br />
&nbsp; &nbsp; 有人说美国方式不好，因为不象福利国家全民覆盖，部分穷人就卖不起医疗保险,所以国家应该给穷人补贴福利。这种人是看到问题，但没提供正确的解决方法。应该承认，倡导政府给穷人提供免费福利或补贴是政治正确，而且占领着道德制高点。但据我所了解的数据和知识，再加上本人也不怎么在意道德，就不介意提出来反对政府给穷人提供免费福利。<br />
<br />
&nbsp; &nbsp; 政府给穷人的福利，无非是靠国家暴力机器，从某个人的口袋强制剥夺来税收，转移放到穷人口袋里。这违背美宪法里自由、平等的原则。如果被征税人不是自愿的，则是政府以民主的方式对这个群体的自由进行了侵犯。为什么小偷在别人口袋里拿钱就违法，政府从民众口袋里拿钱就可以呢？<br />
 <br />
&nbsp; &nbsp; 其实，我认为给穷人提供免费福利是好事，但这事不应该由政府来强制办，而应该由民间私人非盈利性机构来办，象比尔盖茨这样的慈善家捐出那么多资金来办慈善，这既是自愿的，也更有效率，也没侵犯任何人的自由和权利，对任何人都是公平的，是最好的解决方法。政府最好的作用，则是刺激和鼓励私人非盈利机构自愿去给穷人提供更多福利。<br />
<br />
以上是比较现实中两种模式的优劣，以下简单地从理论上证明欧洲国家补贴式福利体制的弊病<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 18世纪英国法学家伦理学家Jeremy Bentham提出：人们一切行为或政府一切措施是遵从增进幸福（ happiness )或减少幸福(happiness)的倾向。简单说就是一种行为如有助于增进幸福，则为正确的；社会全体的幸福是由组成此社会的个人的幸福的总和。社会的幸福是以最大多数的最大幸福来衡量的。如果增加最大多数的最大幸福的制度和政策则为正确。<br />
 <br />
&nbsp; &nbsp; 当权的总是很喜欢拿这个说法来解释自己的高征税行为。希望政府掌握更多的资源和更大权力空间，以便对社会施以更大控制，这才是当权派真正出发点。支持国家福利体系的人大都很支持Jeremy Bentham的理论，认为由国家对高收入群体征税，把税收转移给低收入群体，能增进社会的总福利和幸福程度，这听起来很对看起来很美，但仔细推敲站不住脚。<br />
 <br />
&nbsp; &nbsp; 首先，幸福(happiness)的定义是什么？幸福可以比较吗？不同年龄、文化、性别、阶层的人对幸福（happiness)的定义不一样，甚至同一个人在不同的时段对幸福的理解也不一样。比如，年轻人可能认为，能升职加薪最幸福，剩女们则认为能赶快嫁个合适的老公最幸福，不少老男人就认为什么时候能把糟糠老婆离掉最幸福，而光棍们则认为啥时候娶上老婆热炕头最幸福。。。。那么这些性质不同的个人幸福，政府何从统计总幸福呢？一个人加一个人等于两个人，一个人加一辆车等于什么呢？<br />
 <br />
&nbsp; &nbsp; 其次，假设民众的幸福都是同种性质的，可以进行统计相加的，但是如何量化衡量呢？假设我有1千万，当前我的幸福值为4，穷人幸福值为1，政府要把我1百万强行转移给穷人办福利。如果我象比尔盖茨那样是个热心慈善家，我觉得办慈善我很happy,那么我的幸福增加到5，穷人拿了我的钱幸福值增加到2，我增加自己幸福的同时也增加了别人的幸福，总福利为7。但假如不幸我是个抠门的葛朗台呢？拿了我1百万，我心痛狂吐血不止，从此烙下病，幸福值从原来的4掉到1，而穷人拿了我的钱幸福值增加到2，社会总幸福为3，比原来的总福利降低了。但是千里之外的政府怎么知道我的幸福值多少呢？ 谁能下这个结论，社会的总体幸福程度因为国家把我这1百万块劫富济贫而提高了呢？？没人能这么说，除了那些希望在这1百万块转移支付中上下其手的政客们！<br />
 <br />
&nbsp; &nbsp; 所以，增进社会总幸福的说法是建立在一个伪命题的基础上的，有小学数学水平的人都能推翻它。这种由道德或幸福论支持的说法，，跟江core的“以德治国”一样模糊不经推敲。<br />
 <br />
&nbsp; &nbsp; 为什么象美国这种体制才是最优呢？国家不办强制福利，由民间非盈利机构来办，这是私人自愿的，可以确认没有减少自愿出钱办福利的人的幸福，而且由于私人是自愿办慈善，理论上推论其个人得到心灵上的愉快而提高幸福；而穷人得到福利，幸福也得到提高。这种情况在经济学上称为帕累托改进（帕累托改进 Pareto improvement，是指一种变化，在没有使任何人境况变坏的情况下，使得至少一个人变得更好 ）。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
<br />
&nbsp; &nbsp; 忘了谁说过一句话：美好而免费的东西，都很可疑。</div>

							
							
							
							
