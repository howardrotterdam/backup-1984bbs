													<h2>回复 8楼 蒙面佐罗 的话题</h2>
						
						
																											<div id="postmessage_495864" class="t_msgfont">首先，这两种税我当然知道。公司的所得税，几乎任何国家都有。但是，公司并不专属于富翁啊。而且，公司的税收多了，公司也会跑掉。个人所得税收94%是可以想象的，公司你这么收，没利润，拿什么扩张？所以，公司所得税必然要很低，而公司的所有者自然就非常有利。<br />
其次，除了遗产继承税，外国人或外资一般是没有税务优惠的。问题是，资本和人都可以走的情况下，人家只要不想交遗产继承税，你又怎么可能收到？<br />
第三，资本家不是随便可以在公司报销个人开支的，中国也一样。美国管的比较严的是吃饭，公司请客必须单独交税。但是管不了的也很多。比方说：我想旅游，怎么报销不难吧？<br />
关键是：你可以用税收来限制金钱，但是不能用税收来限制权力。掌握了大公司的人，就掌握了权力，他只要发展这个公司就好了，不必有大量收入。其实你有大量收入你也花不掉，（如果你不是大资本家）你也得投资或者储蓄起来，但是，你就得先纳税了，而且是反复形成收入反复纳税。<br />
（为了鼓励投资，也有免税，比方说你可以免税买一部分股票，等卖出再交税，但这毕竟是一部分。）<br />
总之，公司所得税和资本利得税总是不能太高的，这也就限制了个人所得税的上限。我们应该降低个人所得税到资本利得税的水平，而不是提高资本利得税到个人所得税的水平。<br />
富人并不害怕高税，因为高税只是对他的利润征税，所有成本都会预先扣除。但穷人要奋斗才能成功，他所付出的个人成本，却很难量化并扣除。所以，高税只能是对个人奋斗惩罚。而一旦人们开始放弃奋斗了，阶级也就凝固了下来，上层社会就更加高枕无忧了。<br />
