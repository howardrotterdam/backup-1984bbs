第二节：污染和妓女是中国的代名词<br />
<br />
 <br />
<br />
<br />
十来年二十年前，污染这个词对于绝大多数国人来说都是陌生的，而现在，它成了中国人最熟悉最流行的词语，可见二十年间江山变化之大。 都市人可能不了解，现在农村中的癌症村非常多，这些村里的中青年人不是偶尔死一个，而是成批的死亡。<br />
<br />
为什么？是因为工业转移，城市要保护环境，污染企业都转移到农村去了，而本来农村那些小化工小染织等等已经是超级污染源，所以，现在农村的污染情况比城市还要恐怖得多。中国的事情就是这么奇怪：把污染企业转移到其它地方，难道中国九百六十万平方公里土地中有一些不属于中国人的？ 一个国家，几乎所有的河流都被污染，几乎所有的土地都被污染，几乎所有的空气都被污染，这真是空前绝后的成就。 <br />
<br />
台湾早在几十年前就得出结论：工业只能在限定的范围内发展，而且工业必须分类布局以便于污染的治理。而现在的大陆仍然是工业遍地开花，就是神仙也没有办法去治理中国的污染问题。 为什么会出现如此情况？难道我们的官员都是二百五吗？不是，原因在于我们的体制要求官员都要去搞GDP，体制要求官员拼命增加税收，有税收就有钱花，有GDP就有官做，在这种情况下，问题永远不可能得到缓解，我们的国土注定要成为死亡地带。 这是一场真正的“生化危机”。 <br />
<br />
