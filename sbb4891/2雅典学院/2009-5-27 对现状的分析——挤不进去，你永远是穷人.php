第七节： 权贵们才是“暂住中国”<br />
<br />
 <br />
<br />
<br />
前两年的某一天，我突然发觉一个现象：现在出国跟二十年前农民进城见世面般容易，同事某某的女儿出国了，某某的儿子也出国了，花些钱就能去欧洲留学了，真是简单。 再仔细瞅瞅，原来官家的子女更是了得，就说咱这国企吧，不谈级别挺高的老总书记们，就是那些处长的子女也普遍出国留学了。 <br />
<br />
现在媒体也披露了，中国出国潜逃的巨贪成千上万，如果公布一下所有“潜逃”的贪官数量，肯定是个骇人听闻的数字，这是官方认定的“贪官”，至于那些将自己的妻儿将自己的财产转移到国外的官员数目，我可以肯定：其总数比一些小国寡民的国家的人口还要多。而他们转移到西方国家的财富规模之巨，已经不是“空前绝后”“史无前例”这些词语能够形容的了。 <br />
<br />
中国的王朝更替频繁，皇帝轮流坐，今年到我家。但是历史上所有的王朝，不论哪个皇帝和官员，不论他们如何折腾，有一点是肯定的：他们必须玩到底，也就是说皇帝及官员们必须和自己的王朝同生同灭，是没有退路的，你玩好了，活得也爽，玩完了，自己的小命包括自己的家族也得和自己的王朝一块儿完蛋。如果权贵们有了“出国”的退路的话，那么历史上如严嵩、和坤等人恐怕就不会那么“清廉”了，因为他们有退路，那还怕啥？尽情地折腾，玩完就走路，所谓“我走之后，哪管它洪水滔天”。 <br />
<br />
现代中国人的更加不幸就在这里，今天的权贵们根本无所畏惧，他们在这片土地上无法无天，他们在这片土地上敲骨吸髓破坏一切毁灭一切吸干一切，他们使人民“生不起，死不起，活不起”，他们把这片土地污染成为“死亡地带”，他们将自己的财富转移到国外，他们让自己的妻儿家属获取外国居住权，他们的家族在这片土地上玩命地掠取财富，他们在这片土地上尽情地享乐和折腾，因为即使洪水滔天，他们也能随时全身而退。 这片土地实际上是一个“Colony”，因为治理这片土地的权力阶层现在或将来注定是外国人，这片土地是一个由拥有外国国籍的官僚权贵阶层统治的具有封建主义、colonialism、资本主义混合体特征的“异形”。 <br />
