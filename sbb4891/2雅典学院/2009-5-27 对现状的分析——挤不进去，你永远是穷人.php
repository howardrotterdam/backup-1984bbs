第六节：知识不能改变命运，平民失去翻身的机会<br />
<br />
 <br />
<br />
<br />
和家乡村里老人聊天时他们常说：还是你们那时读书好，有分配哦。其实我这等草民只是毕业后仅能够安家糊口，还算没连累父母。我绝对不赞成“有分配”之说，但现在的农村确实又回到“读书无用”的时代了。 <br />
<br />
上世纪八十年代，是我读小学中学的时候，那时有过“读书无用”的争论，不过那仅仅是针对一小部分群体而言的，比如对那时有城镇户口的孩子来说，读书还真的无用，反正将来总有工作，你大学毕业后分配的工作往往还不如高中毕业直接分配的工作待遇好，如一些大型国企的职工子弟等等。 <br />
<br />
而对我们这些农村户口的孩子而言，读书几乎是唯一可能改变命运的道路，那时从小学到大学的学费都不高，就我这里来说，除了少数非常困难的家庭之外，都是可以供得起孩子读书的，那时中小学的学生流失也比较严重，原因多为他们学习成绩太差，实在不愿意再读，因为那个时候中考高考的录取率实在是太低了，他们根本没可能升学。那时的教育总体环境也确实很差，我读小学时教室里一到下雨天都可以养鱼，但是对农村孩子来说，只要你想读书，愿意读书，还是可能有一个“美好”的未来的。 <br />
<br />
现在呢？首先要承认现在社会发财致富的机会应该说是大大的增加了，读书并不是“独木桥”，然而对于任何国家和社会来说，发财总是少数人的机会，创业更是少数人的成功之路，比尔.盖兹的成功并不能代表美国其它勤学青年没有能耐。如果一个国家的民众对教育投资结果的感觉普遍是负数的话，那么这个社会必定有大问题。而对于中国的平民来说，对教育投资的普遍感觉已经是负数了。 <br />
<br />
现在的读书成本究竟有多高早已有无数的版本，中国青少年研究中心发布报告显示：中国30年来高等教育学费上涨了25倍。可以说，上个学校没有几万元你甭想拿到毕业证，对于年收入一两千元的农民来说，这是几十年拼命劳苦才能够积攒到的数字。 <br />
<br />
这仅仅是大学的花费，小学中学的花费也不小，我的印象中没有人认真统计这方面的资料，原因可能是各地差别太大吧，不好统计，但是有一点，既然现在教师的待遇有了很大提高，而国家并没有对教育投入多少，羊毛总是出在羊身上，不管你是城市还是农村，一概不能免俗，我这里想读县中的高中部，几年前的价格就是差一分一万元，还要有门路才行，有人会说：干嘛花钱读？县中不额外收钱的高中班就那几个，其它全是收费的，这叫“请君入瓮”，教育是要好的环境的，不然大家为何总是挤破头读重点中小学？现在教师正常课程不愿教，热衷于补课，你敢不补吗？要你掏钱给孩子进课外班，你敢不出这血吗？不管城市还是农村的中小学，三头两头让学生交费用，名目五花八门，真是难为我们的人类灵魂工程师能想得出来那么多花样。 <br />
<br />
在中国什么职业好？公务员，事业单位，大型垄断国有企业，水电气等等特殊国有企业单位，确实，公务员是公开招聘的，事业单位等等职位也是招聘的，先统考再面试，但地球人都知道这里面的潜规则，我有不少同事也进入政府机关，其它同事为何不去考？凡去考试的人之前都找好关系了，这才去考试去面试，考试也许是真，面试就是做个样子。自然，我不能说平民一定进不了政府机关。 <br />
<br />
今天的中国，孩子的就业能力是由其老子或家族掌握的权力来决定的。今天的中国除了公务员事业单位某些大型国企等少数职业可以给你好的福利待遇之外，还有多少职业能够给予？不要太抱怨私有企业，一些私有企业确实是够黑的，但是就总体情况来说，他们要是按“规矩”办事的话，早就死光光了。 <br />
<br />
公务员等等之类的超高薪职业现在基本上给利益集团垄断了，我们的现实就是“官员世袭化，公务员世袭化”。 现在大学生就业薪水竟然低到几百元的地步了，我估计，薪水低到如此地步的职业恐怕也不可能给你各种国家规定的劳保福利了。一方面，获取国家权利的部门及企业的员工薪水之高令人乍舌，今天中国相当一部分权利部门员工的生活水准早已达到或超过港台韩国这些新兴发达国家和地区的生活水准。而另一方面，中国又是一个廉价劳动力极其丰富的国家，劳动力便宜得跟奴工一样，因此而获得众多的外国投资，我们并且以此为荣，以大学生十几年寒窗苦毕业后却只能获取几百元的薪水为荣。 前两年南方地区出现了所谓的“民工荒”，我不得不佩服某些国人的无耻程度，在这个国家里有数亿人迫切需要一个工作来养家糊口时，而一些人竟然说用工紧缺，要求民工们不要过分挑剔，而就在如此低廉的劳动力的情况下，竟仍然有层出不穷的奴工黑窑工事件出现，真是让人惊叹。 而去年以来因为经济危机造成大量企业倒闭，又造成民工大量失业，本来这个国家的用人成本已经低贱到如此地步，却仍然有人将企业倒闭归罪于“劳动力成本增加”，让人不得不惊叹这个国家某些人的寡廉鲜耻。 <br />
<br />
读个十几年书能把自己的家庭读得倾家荡产，那可真是到了卖儿卖女的地步，如果这样的情况下还是毕业即失业，毕业后还不能够养活自己，那么现在普遍发生在女大学生中的性交易现象就不难理解了。 <br />
<br />
贫困并不可怕，任何国家和社会都有贫困群体，可怕的是将平民翻身的机会彻底堵死，而现在的现实就是普通老百姓的晋升机会已经基本被堵死，贫民的后代永远是贫民，官员的后代永远是官员。 民众不信神，民众不相信因果报应，民众没有希望和未来，那么民众还能干什么？这个社会的治安为何混乱到如此地步？为何群体性事件总是以打/砸/抢收场？这是很简单的道理。 <br />
<br />
 <br />
<br />
<br />
挤不进去，你永远是穷人<br />
<br />
――中国现状之分析<br />
