第四节：土地是权力阶层的主要掠财源泉<br />
<br />
 <br />
<br />
<br />
我认为，二十世纪末本世纪初影响中国最大的事件恐怕就是波澜壮阔的“圈地运动”了，这不仅仅是中国的历史大事，也是世界性的历史事件，我想未来的世界历史将会给它一个应有的地位的，对于这场运动对中国的影响，可以说是“十年之间，家不见家，国不见国”。 圈地运动的目的何在？你可以说是经济发展的需要，也可以说是掠财的需要，我认为，在圈地运动的初期，因为中国自身环境的局限性，多数国人还没有看出土地的巨大财富价值，那个时候圈地运动的主要目的除了官员政绩的需要外，不能否认发展经济的因素，然而就如股票的疯狂一样，当特权阶层普遍发现土地的巨大利益后，圈地运动就变得极其残酷了，从这以后，我认为圈地的唯一主要目的就是掠夺财富，有时候这几乎成了ZF唯一感兴趣的事情。既然资本家为了百分之三百的利润不惜走上绞刑台，而这场圈地运动的利润早已不是百分之三百的事了，而且特权阶层还拥有资本家所不具备的完全掌握国家机器的权力，所以，当今的圈地运动是一场不可能被阻止的历史事件。 <br />
<br />
通过圈地而获取的财富是今天中国暴富集团获取财富的最大源泉，将来仍然是特权阶层掠财的主要方式之一，从城市到农村，从沿海到内陆，谁掌握了土地，意味着谁就有取之不尽的财富，即使是今天的乡级“首长”，即使是一个小小的村长也常常可以敛财百万千万甚至上亿，原因就是土地的使用权甚至可以任由一个村长支书来决定，由他任意使用，为了这个目的一个村长往往可以通过土地售卖分赃集团调动可怕的国家力量来实现他的目标，这也是乡村变乱的一个主要因素。 <br />
<br />
疯狂的圈地运动造成对资源及环境的极大浪费和破坏，这是当今利益集团对民族的生存和未来所犯下的严重罪行，也是对全人类犯下的严重罪行。</div>

							
							
							
							
