第一节：国民收入的一半以上用来供养ZF工作人员<br />
<br />
 <br />
<br />
<br />
我国需要民众供养的ZF人员究竟有多少？这绝对是一个世界之谜，有关方面的统计是“截止2003年底,公务员的总数是636.9万人”，请无论如何记住一点：在ZF机构的人员中，公务员所占比例并不大，2005年官方非正式估计“吃财政饭的人员约有4500万人以上，另外，还有500万人仰赖于政府赐予的权利实行自收自支”。<br />
<br />
我认为财政饭有三层意思，第一种财政饭是法理许可的财政支出，比如公务员的部分帐面工资就是合法的吃财政饭。<br />
<br />
第二种财政饭是ZF有关规定赋予的“自收自支”权利而获取的，如工商，城管等部门庞大收入的一部分。<br />
<br />
第三种财政饭是法理法规皆不许可，但是却被社会完全默认的财政来源，也就是每一个ZF部门皆在干着的收费经营搞实业拿干股，这个是ZF默认也是被社会默许的“潜规则”范围内的“财政饭”，而这个财政饭要比合法的财政饭大得多，简直是深不见底。 强调一点，以上三种ZF收入皆是被社会默许的财源，与当今国人思维中的“贪污腐败受贿”并不是一个概念。<br />
<br />
这些资料就能够大致得出老百姓需要赡养的人员总数了吗？绝对不是，我可以举几个例子以证明：我所在的国有企业（可以依此来类推ZF情况），几乎每一个部门每一个办公室都有“借调”人员，什么是“借调”人员？就是不属于编制内，但实际就是靠他们干活的员工，这些人都是隐身人，是不在正常的统计报表内的，但是数量惊人。在城市街头经常可以看到一辆小型客货两用车突然出现，上面跳下来几个穿制服的城管队员和不穿制服的农民工，一阵鸡飞狗跳后，在城管的指挥下，农民工将缴获的战利品搬上汽车扬长而去，大家注意了，城管是可以吃部分财政饭的人员，而这种农民工就是俗称“白员”中的最底下最贱层。高级的白员是什么人？是有头面的小官员的家属们，他们被安插在ZF各衙门里，活得也不比一般ZF小职员差，慢慢也会“进化”为正式的ZF员工，还有一种非常特殊的“白员”群体，比如交警部门的暂扣车辆停车场，很多（我不能说全部）交警部门的停车场都是由地方上的地痞流氓“管理”的，你可以贿赂他们来要回自己被扣的车辆，这些人实际上也是一种非常特殊的“白员”，是黑白结合的一种常态。 大家注意观察一下就会发现：ZF部门，国有垄断企业，大部分的事业单位，它们的正式员工多是“皇帝般”的员工，多是不用太劳神太有压力地工作的，至少在本职工作上，脏活苦活累活伤脑筋的活都是雇佣非正式员工来干，也就是上面所讲的“借调人”、“白员”，而现在这些人中的大部分已经成了理所当然必须雇佣的“合同工”了。<br />
<br />
可能每一个上述部门的正式员工后面都有一串的“另类白员”，员警的后面有辅警，有保安，有联防，电信局供电局整天爬电线杆的不会是正式工，现在连环卫工人都雇人扫马路了，可想而知，我们需要供养的ZF人员数量有多庞大，我国ZF部门的“收入”有多骇人听闻。 <br />
<br />
我举这些例子目的很简单：除了包括600多万公务员在内的，在党政机关中“国有”身份的员工1000余万人，以及科教文卫金融等等事业单位3000余万员工外，ZF机构里还有数量极其庞大的靠“合法”或“非法”的“财政”吃饭的人，这些人的数量比公务员本身的数量还要多得多。严格的说，那些所谓行业协会也是属于吃自收财政饭的群体，因为它们之所以有威慑力四处敲诈勒索获取丰厚利益，还是要靠ZF赐予的老虎皮。 现在大家应该有个大致的印象了，我国除了四千多万国家承认的吃财政饭的人员外，还有数量惊人的属于潜规则范围内的吃财政饭的人员，我没有能力得出他们的实际数量，但是，两千万人总是要有的，我不认为这个数字被夸大了。 <br />
<br />
请注意，农村这块地方还没计算呢。根据官方数字，1995年，乡、镇、村还有吃三提五统的1400万人，还不包括 700万村官，又说是500万村官，我就不明白了，ZF怎么连自己的职员都统计不清楚？经过改革，现在的数量也许少了，我只能说但愿如此，问题是：难道把这些“乡村皇帝”们都饿死吗？ 好几千万人的吃财政饭大军需要多少财富来供养？根据官方公布资料，2007年国内生产总值246637亿，2007年全国税收收入完成近五万亿元，而根据中/央党/校教授、知名学者周天勇的估计，我国 2007年实际财政收入达到9万亿元人民币。<br />
<br />
这些“数字蛋糕”怎么分配？我可以提供其它一些资料以分析：“八五”期间，公车车辆消费占到全部国家财政支出的38%，2003年国家财政收入约为 21715亿元，而由国家财政、预算外资金、企业和村民交费和列支成本支出的行政事业供养费用高达14266亿元，加上公安和武装员警的 1500亿，达15766 亿元。占2003年GDP的13.52 %，由这些被供养的公务人员所消耗；国家预算收入、有统计的预算外 5000亿收入、没有被统计的 3000 亿预算外收入，<br />
<br />
整个国家总计支出37960亿中的 37.58%，由被供养的行政公务人员所消耗；又有政府部门统计表明：官员公车消费公款吃喝公费出国年花费达9000 亿元。请注意，以上这些都是官方的资料。 从以上眼花缭绕的官方资料中，我们不难得出结论：国家财政收入的大部分都被ZF人员消耗掉了。 <br />
<br />
公布了官方的资料，我谈谈自己的看法，以官方认可的六百多万公务员为例，这些人一年实际需要多少钱供养？中组部原部长张**说：政治弊端是“官多为患”，一个省有四五十个省级干部，几百个乃至上千个地厅级干部，一个县几十个县级干部。中国共有 33个省级行政单位（扣除台湾），2,861 个县级行政区划单位。那么由此推算：全国各地就至少有一千多个省级干部，两万个厅级干部，好几万到十来万个县级干部，至于京城及军、警等等方面有多少官员我就不能得知了。 现在沿海地区ZF部门里一般职员年收入十万元是很平常的，属于中低收入，在很多地方这是低收入，很多“发达”地方的ZF普通公务员年收入二十万元也极普遍，年终发个十万元奖金不是什么稀奇事，也不是仅仅税务这些部门才有这个财力。<br />
<br />
注意，这是“合法”的收入，是可以拿出来“晒”的收入，养一个公务员绝对不止薪水这一块，医疗交通吃喝拉撒贪污受贿等等需要供养的地方太多了，现在很多地方ZF小职员每月的车贴就比民工辛苦一个月的薪水还要多， ZF人员买个床单内衣都要开个发票去报销。可以说，国家除了没给ZF职员配备法律意义上的配偶外，其它都是必须提供的。 至于那些村王乡王县王市王省王等等的书记及某长们，这些人及其家族一年需要多少财富来赡养？简直没法想象。 <br />
<br />
大家现在自己可以估算一下：一千多个省级干部及其家庭一年要花费多少钱供养？两三万个厅级干部及其家庭需要多少钱供养？好几万到十来万个县级干部及其家庭又需要多少钱供养？再统计全国好几百万金光闪闪的公务员又需要多少财富来为他们全方位服务？ 党政机关其它几百万上千万人员呢？还有三千余万事业单位人员，还有几百万村官一千多万乡村混饭吃的人员，还有千百万个ZF雇佣的形形色色的白员。党政机关其它几百万人难道就是省油的灯？事业单位难道就是后娘养的吗？你以为街道办事人员收入就低？我可以完全负责任地告诉大家：很多地方街道的工作人员月薪万元根本就很平常，大家可以去调查。<br />
<br />
因此我可以得出结论：中国国民收入的至少半数以上都装进ZF人员的口袋里去了。中国就是一个超级二元社会，一部分相对数量很少但是绝对数量很庞大的中国人早已过上发达国家水准的生活，而其它中国人则是标准的贫穷国家的国民。 最近得知，某地海关因为经济不景气，收入被砍掉一半，结果此地海关全体人员近乎“疯狂”，办理任何事情不把钱送到位就不办事。很难想象：一个国家的工作人员的薪水可以象弹簧一样大幅度增加或减少。</div>

							
							
							
							
