<br />
第一节：国民收入的一半以上用来供养ZF工作人员<br />
<br />
我国需要民众供养的ZF人员究竟有多少？这绝对是一个世界之谜，有关方面的统计是“截止2003年底,公务员的总数是636.9万人”，请无论如何记住一点：在ZF机构的人员中，公务员所占比例并不大，2005年官方非正式估计“吃财政饭的人员约有4500万人以上，另外，还有500万人仰赖于政府赐予的权利实行自收自支”。<br />
<br />
我认为财政饭有三层意思，第一种财政饭是法理许可的财政支出，比如公务员的部分帐面工资就是合法的吃财政饭。第二种财政饭是ZF有关规定赋予的“自收自支”权利而获取的，如工商，城管等部门庞大收入的一部分。第三种财政饭是法理法规皆不许可，但是却被社会完全默认的财政来源，也就是每一个ZF部门皆在干着的收费经营搞实业拿干股，这个是ZF默认也是被社会默许的“潜规则”范围内的“财政饭”，而这个财政饭要比合法的财政饭大得多，简直是深不见底。<br />
<br />
强调一点，以上三种ZF收入皆是被社会默许的财源，与当今国人思维中的“贪污腐败受贿”并不是一个概念。<br />
<br />
这些数据就能够大致得出老百姓需要赡养的人员总数了吗？绝对不是，我可以举几个例子以证明：我所在的国有企业（可以依此来类推ZF情况），几乎每一个部门每一个办公室都有“借调”人员，什么是“借调”人员？就是不属于编制内，但实际就是靠他们干活的员工，这些人都是隐身人，是不在正常的统计报表内的，但是数量惊人。在城市街头经常可以看到一辆小型客货两用车突然出现，上面跳下来几个穿制服的城管队员和不穿制服的农民工，一阵鸡飞狗跳后，在城管的指挥下，农民工将缴获的战利品搬上汽车扬长而去，大家注意了，城管是可以吃部分财政饭的人员，而这种农民工就是俗称“白员”中的最底下最贱层。高级的白员是什么人？是有头面的小官员的家属们，他们被安插在ZF各衙门里，活得也不比一般ZF小职员差，慢慢也会“进化”为正式的ZF员工，还有一种非常特殊的“白员”群体，比如交警部门的暂扣车辆停车场，很多（我不能说全部）交警部门的停车场都是由地方上的地痞流氓“管理”的，你可以贿赂他们来要回自己被扣的车辆，这些人实际上也是一种非常特殊的“白员”，是黑白结合的一种常态。<br />
<br />
大家注意观察一下就会发现：ZF部门，国有垄断企业，大部分的事业单位，它们的正式员工多是“皇帝般”的员工，多是不用太劳神太有压力地工作的，至少在本职工作上，脏活苦活累活伤脑筋的活都是雇佣非正式员工来干，也就是上面所讲的“借调人”、“白员”，而现在这些人中的大部分已经成了理所当然必须雇佣的“合同工”了。可能每一个上述部门的正式员工后面都有一串的“另类白员”，警察的后面有辅警，有保安，有联防，电信局供电局整天爬电线杆的不会是正式工，现在连环卫工人都雇人扫马路了，可想而知，我们需要供养的ZF人员数量有多庞大，我国ZF部门的“收入”有多骇人听闻。<br />
<br />
我举这些例子目的很简单：除了包括600多万公务员在内的，在党政机关中“国有”身份的员工1000余万人，以及科教文卫金融等等事业单位3000余万员工外，ZF机构里还有数量极其庞大的靠“合法”或“非法”的“财政”吃饭的人，这些人的数量比公务员本身的数量还要多得多。严格的说，那些所谓行业协会也是属于吃自收财政饭的群体，因为它们之所以有威慑力四处敲诈勒索获取丰厚利益，还是要靠ZF赐予的老虎皮。<br />
<br />
现在大家应该有个大致的印象了，我国除了四千多万国家承认的吃财政饭的人员外，还有数量惊人的属于潜规则范围内的吃财政饭的人员，我没有能力得出他们的实际数量，但是，两千万人总是要有的，我不认为这个数字被夸大了。<br />
<br />
请注意，农村这块地方还没计算呢。根据官方数字，1995年，乡、镇、村还有吃三提五统的1400万人，还不包括 700万村官，又说是500万村官，我就不明白了，ZF怎么连自己的职员都统计不清楚？经过改革，现在的数量也许少了，我只能说但愿如此，问题是：难道把这些“乡村皇帝”们都饿死吗？<br />
<br />
<br />
好几千万人的吃财政饭大军需要多少财富来供养？根据官方公布数据，2007年国内生产总值246637亿，2007年全国税收收入完成近五万亿元，而根据中/央党/校教授、知名学者周天勇的估计，我国 2007年实际财政收入达到9万亿元人民币。<br />
<br />
这些“数字蛋糕”怎么分配？我可以提供其他一些数据以分析：“八五”期间，公车车辆消费占到全部国家财政支出的38%，2003年国家财政收入约为 21715亿元，而由国家财政、预算外资金、企业和村民交费和列支成本支出的行政事业供养费用高达14266亿元，加上公安和武装警察的 1500亿，达15766 亿元。占2003年GDP的13.52 %，由这些被供养的公务人员所消耗；国家预算收入、有统计的预算外 5000亿收入、没有被统计的 3000 亿预算外收入，整个国家总计支出37960亿中的 37.58%，由被供养的行政公务人员所消耗；又有政府部门统计表明：官员公车消费公款吃喝公费出国年花费达9000 亿元。请注意，以上这些都是官方的数据。<br />
<br />
从以上眼花缭绕的官方数据中，我们不难得出结论：国家财政收入的大部分都被ZF人员消耗掉了。<br />
<br />
公布了官方的数据，我谈谈自己的看法，以官方认可的六百多万公务员为例，这些人一年实际需要多少钱供养？中组部原部长张**说：政治弊端是“官多为患”，一个省有四五十个省级干部，几百个乃至上千个地厅级干部，一个县几十个县级干部。中国共有 33个省级行政单位（扣除台湾），2,861 个县级行政区划单位。那么由此推算：全国各地就至少有一千多个省级干部，两万个厅级干部，好几万到十来万个县级干部，至于京城及军、警等等方面有多少官员我就不能得知了。<br />
<br />
<br />
现在沿海地区ZF部门里一般职员年收入十万元是很平常的，属于中低收入，在很多地方这是低收入，很多“发达”地方的ZF普通公务员年收入二十万元也极普遍，年终发个十万元奖金不是什么稀奇事，也不是仅仅税务这些部门才有这个财力。注意，这是“合法”的收入，是可以拿出来“晒”的收入，养一个公务员绝对不止薪水这一块，医疗交通吃喝拉撒贪污受贿等等需要供养的地方太多了，现在很多地方ZF小职员每月的车贴就比民工辛苦一个月的薪水还要多， ZF人员买个床单内衣都要开个发票去报销。可以说，国家除了没给ZF职员配备法律意义上的配偶外，其它都是必须提供的。<br />
<br />
至于那些村王乡王县王市王省王等等的书记及某长们，这些人及其家族一年需要多少财富来赡养？简直没法想象。<br />
<br />
大家现在自己可以估算一下：一千多个省级干部及其家庭一年要花费多少钱供养？两三万个厅级干部及其家庭需要多少钱供养？好几万到十来万个县级干部及其家庭又需要多少钱供养？再统计全国好几百万金光闪闪的公务员又需要多少财富来为他们全方位服务？<br />
<br />
党政机关其他几百万上千万人员呢？还有三千余万事业单位人员，还有几百万村官一千多万乡村混饭吃的人员，还有千百万个ZF雇佣的形形色色的白员。党政机关其他几百万人难道就是省油的灯？事业单位难道就是后娘养的吗？你以为街道办事人员收入就低？我可以完全负责任地告诉大家：很多地方街道的工作人员月薪万元根本就很平常，大家可以去调查。<br />
<br />
因此我可以得出结论：中国国民收入的至少半数以上都装进ZF人员的口袋里去了。中国就是一个超级二元社会，一部分相对数量很少但是绝对数量很庞大的中国人早已过上发达国家水准的生活，而其他中国人则是标准的贫穷国家的国民。<br />
<br />
最近得知，某地海关因为经济不景气，收入被砍掉一半，结果此地海关全体人员近乎“疯狂”，办理任何事情不把钱送到位就不办事。很难想像：一个国家的工作人员的薪水可以象弹簧一样大幅度增加或减少。<br />
<br />
第二节：国民经济是由权力阶层操纵的非正常经济<br />
<br />
<br />
中国的经济组成笼统地说是由公有经济、外资和私有经济组成，但是所谓的公有私有经济形式已经完全变味，公有经济未必公有，私有经济未必私有，实际上皆是由特权阶层操纵着，它们是由特权阶层操纵的非正常经济，勉强可以称之为官僚权贵资本主义经济，因为它比通常所称的权贵资本主义经济更加特权化。<br />
<br />
公有经济的最主要表现形式是垄断国有企业，这种垄断形式有两种，一是电信石油之类的在国内外仍然有一定竞争对象的企业，另一种是水电气之类的完全垄断的经营实体。<br />
<br />
有一段时期，公有经济实体里的职位还是一个鸡肋，而现在它们却是香饽饽，电信石油烟草之类靠着高度垄断和半执法者的地位以超烂的管理却能获取骇人听闻的利润，而水电气之类的经营管理者更是以狂赌烂嫖的管理方式照样获得无法形容的丰厚利益，即使它们年年巨额亏损，以至于曾经代表高尚体面的外资白领职业完全黯然失色，现在谁还好意思以外资白领职业为荣呢？ <br />
<br />
其他国家也有中国这两种国有经济形式，一种是ZF认为需要国有的如核工业之类的企业，一种是供水供电之类的服务企业。但是这类国有经济是被压缩到最小范围内，更要接受公众的严格监控，总之，不能因为你的特殊性就骑到民众头上去，而中国这些公有经济实体却是实实在在地骑到民众头上作威作福了。<br />
<br />
公有经济实体为何能如此？是因为公有经济实际上已经完全成为官僚权力集团的私家后院，只不过挂着公有名义的羊头而已，如果说普通老百姓做公务员的机会已经很小，那么你想进入待遇极其丰厚工作极其轻松的垄断公有经济实体成为一个“正式”的员工则是更难（请注意“正式”这个词，下文将会解释），因为这个公有实体已经基本上对老百姓关上大门。<br />
<br />
国有企业的真实面目是拥有国有企业特殊权力的私有企业，通常称为官僚权贵资本，我们的还要加上有中国特色的权力，有了这种权力的官僚资本经济实体对于其拥有者来说等于有了一台光速印钞机。<br />
<br />
当今国有经济实体还有一个原罪，那就是残酷剥削“合同工”，如果你能做一个光荣的垄断国企正式员工，那你发达了，你可以躺在那里享受惊人的丰厚待遇，如果你只是公有经济实体里的临时工，那么你只是一头干活的驴子，从电信烟草到水电气交通等等各类公有企业都招募了巨量的临时工人，给予其和正式工相比微不足道的薪水和待遇，可以说，国有垄断企业之所以有那么高的利润，也和雇佣了很多只需要给予微薄待遇的合同工有很大的关系，如果没有临时工合同工，水电气交通等ZF经营实体简直难以想象还要亏损到什么程度，是国有经济实体中“非正式工”的汗水把正式工的钱包浇灌的无比丰厚，真是同处一片天地却是冰火两重天，这是令人发指的剥削，是完全违反人类基本道德准则的行为。<br />
<br />
私有制的蓬勃发展极大的冲击了这个社会的一潭死水，然而对于这种极具中国特色的私有经济是要打个大大的问号的，因为中国这种私有制有别于通常所称的私有制，它的主要形式仍然是官僚资本，由特权阶层控制的私有经济占据了私有区域的最主要部分。<br />
<br />
<br />
特权阶层通过掌握国家权力而获取自己经营上的便利，如每一个国有经济实体的背后都有很多特权阶层拥有的私有企业为其“配套”服务，这种“服务”的利润自然是很高的，这是转移国家资产的一种微妙方式，掌握国家权利的阶层将ZF工程项目交给自己阶层经营的实体承做，这块区域是今天中国暴发户产生的主要源泉之一。而各个ZF衙门利用自己区域范围的管理权力垄断了这些区域的产业经营权，不容民众染指或者极力打压普通民众的正当经营，至于那些“纯粹”的草根私有经济实体则时刻面临ZF各部门的“法规”威胁，如环境保护，税收，劳动保障，不规范的法律条款随时可以要你的命，迫使你乖乖的卷缩在特权阶层的淫威下苟且生存。<br />
<br />
外资尤其是西方资本的进入可以说极大的推动了中国的进步，但是对中国的负面作用也越来越明显，一是环境污染的转移，二是西方面对利益的诱惑丧失了他们的道德标准而纵容黑暗的肆虐。我这里就有一个很大的外资项目----扬子―巴斯夫化工项目，它的污染是显而易见的，而其配套企业的污染更是惊人，但是扬巴这个项目如果建在德国恐怕就不是这个样子了，今天中国的市场对西方确实有很强的诱惑力，甚至可以用来要挟西方，资本家的根本目标就是利润，这种诱惑迫使西方人经常放弃他们的道德和价值观。<br />
<br />
这里提一下我国一种特有的经济形式，就是ZF建筑里的楼台馆所，大家应该注意到，在中国，屁大的ZF机构都要建个大楼，虽然屁大的官员都拥有面积超大装修超豪华的办公场所，但是在如此情况下各个ZF大楼仍然有巨量多余的空间，这个空间自然就用来经营了，或出租或自营，大家不要小看这个区域范围内的经济力量，再加上那些数量极其惊人，由ZF部门、社会事业部门及军、警等所拥有的商业建筑，这些是ZF利益集团的很可观的一笔“额外收入”。<br />
<br />
第三节：土地是权力阶层的主要掠财源泉<br />
<br />
我认为，二十世纪末本世纪初影响中国最大的事件恐怕就是波澜壮阔的“圈地运动”了，这不仅仅是中国的历史大事，也是世界性的历史事件，我想未来的世界历史将会给它一个应有的地位的，对于这场运动对中国的影响，可以说是“十年之间，家不见家，国不见国”。<br />
<br />
圈地运动的目的何在？你可以说是经济发展的需要，也可以说是掠财的需要，我认为，在圈地运动的初期，因为中国自身环境的局限性，多数国人还没有看出土地的巨大财富价值，那个时候圈地运动的主要目的除了官员政绩的需要外，不能否认发展经济的因素，然而就如股票的疯狂一样，当特权阶层普遍发现土地的巨大利益后，圈地运动就变得极其残酷了，从这以后，我认为圈地的唯一主要目的就是掠夺财富，有时候这几乎成了ZF唯一感兴趣的事情。既然资本家为了百分之三百的利润不惜走上绞刑台，而这场圈地运动的利润早已不是百分之三百的事了，而且特权阶层还拥有资本家所不具备的完全掌握国家机器的权力，所以，当今的圈地运动是一场不可能被阻止的历史事件。<br />
<br />
通过圈地而获取的财富是今天中国暴富集团获取财富的最大源泉，将来仍然是特权阶层掠财的主要方式之一，从城市到农村，从沿海到内陆，谁掌握了土地，意味着谁就有取之不尽的财富，即使是今天的乡级“首长”，即使是一个小小的村长也常常可以敛财百万千万甚至上亿，原因就是土地的使用权甚至可以任由一个村长支书来决定，由他任意使用，为了这个目的一个村长往往可以通过土地售卖分赃集团调动可怕的国家力量来实现他的目标，这也是乡村变乱的一个主要因素。<br />
<br />
疯狂的圈地运动造成对资源及环境的极大浪费和破坏，这是当今利益集团对民族的生存和未来所犯下的严重罪行，也是对全人类犯下的严重罪行。<br />
<br />
第四节：知识不能改变命运，平民失去翻身的机会<br />
<br />
和家乡村里老人聊天时他们常说：还是你们那时读书好，有分配哦。其实我这等草民只是毕业后仅能够安家糊口，还算没连累父母。我绝对不赞成“有分配”之说，但现在的农村确实又回到“读书无用”的时代了。<br />
<br />
上世纪八十年代，是我读小学中学的时候，那时有过“读书无用”的争论，不过那仅仅是针对一小部分群体而言的，比如对那时有城镇户口的孩子来说，读书还真的无用，反正将来总有工作，你大学毕业后分配的工作往往还不如高中毕业直接分配的工作待遇好，如一些大型国企的职工子弟等等。<br />
<br />
而对我们这些农村户口的孩子而言，读书几乎是唯一可能改变命运的道路，那时从小学到大学的学费都不高，就我这里来说，除了少数非常困难的家庭之外，都是可以供得起孩子读书的，那时中小学的学生流失也比较严重，原因多为他们学习成绩太差，实在不愿意再读，因为那个时候中考高考的录取率实在是太低了，他们根本没可能升学。那时的教育总体环境也确实很差，我读小学时教室里一到下雨天都可以养鱼，但是对农村孩子来说，只要你想读书，愿意读书，还是可能有一个“美好”的未来的。<br />
<br />
现在呢？首先要承认现在社会发财致富的机会应该说是大大的增加了，读书并不是“独木桥”，然而对于任何国家和社会来说，发财总是少数人的机会，创业更是少数人的成功之路，比尔.盖兹的成功并不能代表美国其他勤学青年没有能耐。如果一个国家的民众对教育投资结果的感觉普遍是负数的话，那么这个社会必定有大问题。而对于中国的平民来说，对教育投资的普遍感觉已经是负数了。<br />
<br />
现在的读书成本究竟有多高早已有无数的版本，中国青少年研究中心发布报告显示：中国30年来高等教育学费上涨了25倍。可以说，上个学校没有几万元你甭想拿到毕业证，对于年收入一两千元的农民来说，这是几十年拼命劳苦才能够积攒到的数字。<br />
<br />
这仅仅是大学的花费，小学中学的花费也不小，我的印象中没有人认真统计这方面的数据，原因可能是各地差别太大吧，不好统计，但是有一点，既然现在教师的待遇有了很大提高，而国家并没有对教育投入多少，羊毛总是出在羊身上，不管你是城市还是农村，一概不能免俗，我这里想读县中的高中部，几年前的价格就是差一分一万元，还要有门路才行，有人会说：干嘛花钱读？县中不额外收钱的高中班就那几个，其它全是收费的，这叫“请君入瓮”，教育是要好的环境的，不然大家为何总是挤破头读重点中小学？现在教师正常课程不愿教，热衷于补课，你敢不补吗？要你掏钱给孩子进课外班，你敢不出这血吗？不管城市还是农村的中小学，三头两头让学生交费用，名目五花八门，真是难为我们的人类灵魂工程师能想得出来那么多花样。<br />
<br />
在中国什么职业好？公务员，事业单位，大型垄断国有企业，水电气等等特殊国有企业单位，确实，公务员是公开招聘的，事业单位等等职位也是招聘的，先统考再面试，但地球人都知道这里面的潜规则，我有不少同事也进入政府机关，其他同事为何不去考？凡去考试的人之前都找好关系了，这才去考试去面试，考试也许是真，面试就是做个样子。自然，我不能说平民一定进不了政府机关。<br />
<br />
今天的中国，孩子的就业能力是由其老子或家族掌握的权力来决定的。今天的中国除了公务员事业单位某些大型国企等少数职业可以给你好的福利待遇之外，还有多少职业能够给予？不要太抱怨私有企业，一些私有企业确实是够黑的，但是就总体情况来说，他们要是按“规矩”办事的话，早就死光光了。<br />
<br />
公务员等等之类的超高薪职业现在基本上给利益集团垄断了，我们的现实就是“官员世袭化，公务员世袭化”。<br />
<br />
现在大学生就业薪水竟然低到几百元的地步了，我估计，薪水低到如此地步的职业恐怕也不可能给你各种国家规定的劳保福利了。一方面，获取国家权利的部门及企业的员工薪水之高令人乍舌，今天中国相当一部分权利部门员工的生活水准早已达到或超过港台韩国这些新兴发达国家和地区的生活水平。而另一方面，中国又是一个廉价劳动力极其丰富的国家，劳动力便宜得跟奴工一样，因此而获得众多的外国投资，我们并且以此为荣，以大学生十几年寒窗苦毕业后却只能获取几百元的薪水为荣。<br />
<br />
前两年南方地区出现了所谓的“民工荒”，我不得不佩服某些国人的无耻程度，在这个国家里有数亿人迫切需要一个工作来养家糊口时，而一些人竟然说用工紧缺，要求民工们不要过分挑剔，而就在如此低廉的劳动力的情况下，竟仍然有层出不穷的奴工黑窑工事件出现，真是让人惊叹。<br />
<br />
而去年以来因为经济危机造成大量企业倒闭，又造成民工大量失业，本来这个国家的用人成本已经低贱到如此地步，却仍然有人将企业倒闭归罪于“劳动力成本增加”，让人不得不惊叹这个国家某些人的寡廉鲜耻。<br />
<br />
读个十几年书能把自己的家庭读得倾家荡产，那可真是到了卖儿卖女的地步，如果这样的情况下还是毕业即失业，毕业后还不能够养活自己，那么现在普遍发生在女大学生中的性交易现象就不难理解了。<br />
<br />
贫困并不可怕，任何国家和社会都有贫困群体，可怕的是将平民翻身的机会彻底堵死，而现在的现实就是普通老百姓的晋升机会已经基本被堵死，贫民的后代永远是贫民，官员的后代永远是官员。<br />
<br />
民众不信神，民众不相信因果报应，民众没有希望和未来，那么民众还能干什么？这个社会的治安为何混乱到如此地步？为何群体性事件总是以打/砸/抢收场？这是很简单的道理。<br />
<br />
第五节：权贵富可敌国，人民一穷二白<br />
<br />
权威部门的报告表明：在金融、外贸、国土开发、大型工程、证券五大领域中担任主要职务的，有百分之八十五至九十是高干子女，实际上已形成了官僚资产阶级。至 2006 年3月底，私人拥有财产（不包括在境外、外国的财产）超过五千万以上的有27310人，超过一亿元以上的有3220人。超过一亿元以上者，有2932人是高干子女，他们拥有资产20450亿元。<br />
<br />
几年前就有正式披露：大陆银行体系存款中的百分之八十，集中由百分之二十的存户持有。我认为这样笼统的统计方法是非常不符合国情的。请看国家统计的数据：2004年年末全年全国农村居民人均纯收入2936元，按年人均纯收入低于668元的标准，年末农村绝对贫困人口为2610万人，按年人均纯收入 669-924 元的标准，年末农村低收入人口为4977万人。而2005年社会科学院分析，如果按照低收入现行标准，即人均年收入低于882元(仅相当于全国农民平均收入水平的1/3)，目前还有8517万人口也属贫困人口。如果按照世界上公认的人均1天1美元以下就属贫困的标准，我国目前还有2.1亿贫困人口。<br />
<br />
什么叫没有解决温饱？就是还没有解决吃饱穿暖的问题，通俗地说就是肚子保持饿着，身体保持冻着，我为何这么废话？因为对于中国很多人来说根本没法想象这种事情，很多饕餮者觉得野菜窝窝头好吃，他们也许会认为那些需要靠野菜窝窝头来糊口的人真是太有口福了。<br />
<br />
那些一年收入不到九百元的八千多万农民，他们的财富实际为零，在这个国家里他们的财富完全可以忽略不计，两亿多每天收入在一美元以下的人，他们拥有的财富也是极其微薄的，中国有***亿农民，其中也有很多人生活比较富裕，但是多数人的状况可以用三个字表达：还活着。也就是勉强使自己的生活运转着，为了这种“运转”不得不背井离乡四处打工或者在自己那点土地上拼死拼活的刨食，这里面有多少艰辛和血泪已经不需要再重复，城市里的贫民或多或少还可以得到一定的国家救济和照顾，这个群体的数量也是很大的，一般的估计在三千万左右，这个统计数字小得真是太具“中国特色”了。我的家乡属于中国中上等水平的农村地区，我这里的农民生活还算过得去，楼房普及，但孩子缀学厉害，主要是因为学费太高，很多家庭觉得供不起读书更划不来，农民如果生大病多数去医院治疗几天之后一般也就出院了，撑一天算一天，环境污染很严重，癌症是普及了，等死而已。<br />
<br />
从以上分析我可以认为：中国近十四亿人口中有近一亿人属于被遗忘的群体，这个世界与他们无关，或者说他们与这个世界无关。有一亿人基本不存在拥有财富的问题，也就是说“财富”这个概念不适用于他们，还有至少四亿人不存在“购买”能力，他们仅仅能保证糊口，商品生产者不必考虑他们的消费能力。<br />
<br />
那么我对中国阶层的分类是这样的：第一个阶层（也是处于最顶端的王者阶层）是由几百个家族组成，他们拥有骇人听闻的财富，是这个国家的掌控者。在他们之下是第二个阶层――地方性的豪族，数量也许是几万家，这些人控制着地方的权力，自然也拥有无与伦比的财产。第三个阶层是由公务员，事业单位人员、国企管理人员、垄断国企人员和私营企业主等这些人中的佼佼者以及顶级白领阶层等这些群体中的人员组成。第四个阶层是生活比较安逸的一般民众，他们经济上还算比较宽裕，但是社会地位不高，对社会没有什么影响力。第五个阶层是由城市平民和农村中生活比较好的农民组成。第六个阶层是贫困群体，也就是四亿没有购买能力的民众。第七个阶层是一亿没有财富的赤贫阶层，第八个阶层就是最后那一亿灾难性赤贫的阶层。<br />
<br />
中国的大部分财富掌握在属于前两个阶层的手里，有六亿人拥有的财富微乎其微，平（贫）民阶层拥有的财富不及第一第二个阶层所拥有的财富多，主要财富掌握在前三个阶层的手里。<br />
<br />
第六节：污染和妓女是中国的代名词<br />
<br />
十来年二十年前，污染这个词对于绝大多数国人来说都是陌生的，而现在，它成了中国人最熟悉最流行的词语，可见二十年间江山变化之大。<br />
<br />
都市人可能不了解，现在农村中的癌症村非常多，这些村里的中青年人不是偶尔死一个，而是成批的死亡。为什么？是因为工业转移，城市要保护环境，污染企业都转移到农村去了，而本来农村那些小化工小染织等等已经是超级污染源，所以，现在农村的污染情况比城市还要恐怖得多。中国的事情就是这么奇怪：把污染企业转移到其他地方，难道中国九百六十万平方公里土地中有一些不属于中国人的？<br />
<br />
一个国家，几乎所有的河流都被污染，几乎所有的土地都被污染，几乎所有的空气都被污染，这真是空前绝后的成就。<br />
<br />
台湾早在几十年前就得出结论：工业只能在限定的范围内发展，而且工业必须分类布局以便于污染的治理。而现在的大陆仍然是工业遍地开花，就是神仙也没有办法去治理中国的污染问题。<br />
<br />
为什么会出现如此情况？难道我们的官员都是二百五吗？不是，原因在于我们的体制要求官员都要去搞GDP，体制要求官员拼命增加税收，有税收就有钱花，有GDP就有官做，在这种情况下，问题永远不可能得到缓解，我们的国土注定要成为死亡地带。<br />
<br />
这是一场真正的“生化危机”。<br />
<br />
中国究竟有多少妓女？不好统计，也不许统计，古时妓女只有较大型城市才存在，后来中小城市也有了，但是“据点”范围很受限制。现在的中国，城市里哪条街道没有妓女营业？别说大中小城市，就是集镇有哪个没有妓女存在的？别说市镇，有些地方连村里都开始普及妓女了。再偏远的地区，国道两旁都是妓馆连立，“妓”满天下。咱们的农村，水电气化没实现，倒是首先普及妓女了，咱们的领导干部天天“闻妓起舞”，“妓”中报国。<br />
<br />
那些高尚的、上层社会的妓女咱不谈，人家都代表咱们了。我们只谈中下层妓女，除了纯粹靠卖为生的妓女，还有无数兼职型妓女，在沿海“发达”地区，白天工厂里打工，晚上“做”的打工妹不计其数，此为“工妓”，价格低到三十元，市场不好，供过于求，生存实在是不容易，可以说，现在农村出去打工的女孩，相当一部分都是以妓为业的，或全职，或兼职。大学每到周末，校园停满了小汽车，都是接美女学生为官僚资本家们服务的，数量更多的女学生则纵横于色场中，此为“学妓”，其数目无法统计。至于街头那些属于中老年享受型的“摸妓”等等不纳入妓女范围，否则范围太大，有“反妓扩大化”之嫌。<br />
<br />
而中国妓女也早已走出国门纵横世界名扬天下，成为一个国际丑闻和问题。<br />
<br />
<br />
中国妓女之普及，这真是一个空前绝后的成就，其数量也许是大几百万，也许是一千来万，但有一点可以肯定：即使全世界其它国家妓女数量的总和也根本无法与中国的妓女数量相比。中国妓女数量占了世界的绝对多数。很难说这是一个值得炫耀的事情。<br />
<br />
可笑的是，即使面对这样的现实，我们仍然宣称禁止色情业，拒绝承认色情产业，这个正验证了我的一贯观点：垃圾国家制定有关环境保护劳动安全以及反色情等方面法规制度的唯一目的就是为了方便捞钱。<br />
<br />
第七节： 权贵们才是“暂住中国”<br />
<br />
前两年的某一天，我突然发觉一个现象：现在出国跟二十年前农民进城见世面般容易，同事某某的女儿出国了，某某的儿子也出国了，花些钱就能去欧洲留学了，真是简单。<br />
<br />
再仔细瞅瞅，原来官家的子女更是了得，就说咱这国企吧，不谈级别挺高的老总书记们，就是那些处长的子女也普遍出国留学了。<br />
<br />
现在媒体也披露了，中国出国潜逃的巨贪成千上万，如果公布一下所有“潜逃”的贪官数量，肯定是个骇人听闻的数字，这是官方认定的“贪官”，至于那些将自己的妻儿将自己的财产转移到国外的官员数目，我可以肯定：其总数比一些小国寡民的国家的人口还要多。而他们转移到西方国家的财富规模之巨，已经不是“空前绝后”“史无前例”这些词语能够形容的了。<br />
<br />
中国的王朝更替频繁，皇帝轮流坐，今年到我家。但是历史上所有的王朝，不论哪个皇帝和官员，不论他们如何折腾，有一点是肯定的：他们必须玩到底，也就是说皇帝及官员们必须和自己的王朝同生同灭，是没有退路的，你玩好了，活得也爽，玩完了，自己的小命包括自己的家族也得和自己的王朝一块儿完蛋。<br />
<br />
如果权贵们有了“出国”的退路的话，那么历史上如严嵩、和坤等人恐怕就不会那么“清廉”了，因为他们有退路，那还怕啥？尽情地折腾，玩完就走路，所谓“我走之后，哪管它洪水滔天”。<br />
<br />
现代中国人的更加不幸就在这里，今天的权贵们根本无所畏惧，他们在这片土地上无法无天，他们在这片土地上敲骨吸髓破坏一切毁灭一切吸干一切，他们使人民“生不起，死不起，活不起”，他们把这片土地污染成为“死亡地带”，他们将自己的财富转移到国外，他们让自己的妻儿家属获取外国居住权，他们的家族在这片土地上玩命地掠取财富，他们在这片土地上尽情地享乐和折腾，因为即使洪水滔天，他们也能随时全身而退。<br />
<br />
这片土地实际上是一个“Colony”，因为治理这片土地的权力阶层现在或将来注定是外国人，这片土地是一个由拥有外国国籍的官僚权贵阶层统治的具有封建主义、colonialism、资本主义混合体特征的“异形”。<br />
<br />
第八节：人格的扭曲，民族的沉沦<br />
<br />
<br />
一个民族不会因为贫穷落后而灭亡，但是民族失去文化和精神后注定要灭亡。<br />
<br />
<br />
“谎言重复一千遍可以成为真理”，在一个“全控型”社会里，说真话者注定要从肉体上或精神上被消灭，整个社会运行中除了谎言就是谎言。无数遍谎言后，甚至连奴才般的民众也会不自觉地认为掌权者的统治是理所当然的。<br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
长期以往，这种国家的国民自觉不自觉的便把谎言当成真理，把真话当成“病态”，而在“真理”和现实的极度扭曲中，这个社会的民众并产生一种“扭曲现象”：他们一方面认定周围一切官员都是贪官污吏，另一方面却又相信某些伟人或远处的高层人物是清廉高尚的，所谓“皇帝是好的，都是下面官员贪腐”。他们一方面无比羡慕西方，崇拜西方的物质和文化，并以自己的子女家属能够去西方为荣，另一方面却又在现实中极端仇视西方，不惜对那些公开宣称欣赏西方的同胞以最恶毒的攻击和伤害。他们一方面了解这个社会的全方位阴暗，另一方面又强烈抵触西方对中国的批评。这是中国人人格扭曲的三个最普遍特征。<br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
在这种“真理”和现实的极度扭曲中，中国人，所有阶层里的大部分人，都为自己编织了一个安慰自己灵魂的理论：西方不也有腐败吗，克林顿不也搞女人吗。这种以西方社会腐败的个案来为中国体制辩解的理论，是国人的精神鸦片。<br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
在无尽的谎言和扭曲的人格下，权贵们一边蹂躏这片土地，一边又高举爱国主义大旗来当作自己的遮羞布，而民众还真的不自觉地响应这种无耻之极的喧嚣，但是民众的“口号爱国”激情却又是建立在“让别人流血流泪”的基础上的，他们会被这种喧嚣激动得热泪盈眶，却又不愿为此受到哪怕一点点的损失。这种扭曲的人格倒是有历史传统的，大家看看明朝那些生吞袁崇焕肉块的民众就明白了。<br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
这个国家另一个空前绝后的扭曲现象――数亿赤贫民众和无数的摩天大楼形成强烈的对比。壮观的城市高楼群成为大部分国人阿Q式骄傲的麻醉剂，那些高楼大厦有那一座地基不是用民众的血泪堆砌起来的？那些以中国城市的“壮观”为荣的人，他们愿意成为“地基”吗？而对于大部分国人来说，那些高楼大厦与他们又有什么关系？这种漠视别人的生命和痛苦，和让别人去流血的“口号爱国”异曲同工。<br />
<br />
<br />
<br />
<br />
<br />
<br />
我们来到这个世界上，从会说话识字开始一直到死亡的那一刻。我们的一生都生活在谎言和两种人格分裂的世界里，我们从小反复被要求热爱这个热爱那个，而教导者自己都不相信这些东西，我们被教育要正直正义遵守道德公德等等，而教导者自己完全明白：在这个社会里，遵守这些规则的人注定要被这个社会毁灭，我们的宣传机器中所宣传的一切，如官员的丰绩，社会的进步，经济的成就，政府的功业等等所谓正面报道，这些宣传连我们的小孩子都知道全都是谎言和欺骗。这个社会教育我们的东西，和这个社会实际运转的完全相反。<br />
<br />
对于这些扭曲现象，我们这个民族已经完全接受，并且以此为正常现象，而那些公开指出“皇帝没穿衣服”的极少数分子反而成了精神不正常的异类。而多数遮遮掩掩的嘀咕“皇帝没穿衣服”的分子，如果他们“跑步上位”进入利益阶层后，马上就舒心地赞道：此地甚好，中国这样已经非常不错了。<br />
<br />
我们这个社会就是以两种完全相反的方式运转的，说的、宣传的、法律条文规定的是一套，实际运行的则是完全相反的另一套，这是我们这个社会的“总规则”。<br />
<br />
这种扭曲的社会和人们的生活方式带来的结果就是整个民族的沉沦，我们以无耻为荣，以人渣为有本事，没有最无耻，只有更无耻，因为只有这样才能在这个社会中生存的更好，才更有机会出人头地。<br />
<br />
这样的民族竟然也喧嚣“民族崛起”，一个“崛起”的国家，其上层社会却整体将自己的财产和国籍转移到西方去，天下有这样的“崛起”吗？<br />
