第五节：权贵富可敌国，人民一穷二白<br />
<br />
 <br />
<br />
<br />
权威部门的报告表明：在金融、外贸、国土开发、大型工程、证券五大领域中担任主要职务的，有百分之八十五至九十是高干子女，实际上已形成了官僚资产阶级。至 2006 年3月底，私人拥有财产（不包括在境外、外国的财产）超过五千万以上的有27310人，超过一亿元以上的有3220人。超过一亿元以上者，有2932人是高干子女，他们拥有资产20450亿元。 <br />
<br />
几年前就有正式披露：大陆银行体系存款中的百分之八十，集中由百分之二十的存户持有。我认为这样笼统的统计方法是非常不符合国情的。请看国家统计的资料：2004年年末全年全国农村居民人均纯收入2936元，按年人均纯收入低于668元的标准，年末农村绝对贫困人口为2610万人，按年人均纯收入 669-924 元的标准，年末农村低收入人口为4977万人。<br />
<br />
而2005年社会科学院分析，如果按照低收入现行标准，即人均年收入低于882元(仅相当于全国农民平均收入水准的1/3)，目前还有8517万人口也属贫困人口。如果按照世界上公认的人均1天1美元以下就属贫困的标准，我国目前还有2.1亿贫困人口。<br />
<br />
 <br />
<br />
<br />
什么叫没有解决温饱？就是还没有解决吃饱穿暖的问题，通俗地说就是肚子保持饿着，身体保持冻着，我为何这么废话？因为对于中国很多人来说根本没法想象这种事情，很多饕餮者觉得野菜窝窝头好吃，他们也许会认为那些需要靠野菜窝窝头来糊口的人真是太有口福了。 <br />
<br />
那些一年收入不到九百元的八千多万农民，他们的财富实际为零，在这个国家里他们的财富完全可以忽略不计，两亿多每天收入在一美元以下的人，他们拥有的财富也是极其微薄的，中国有***亿农民，其中也有很多人生活比较富裕，但是多数人的状况可以用三个字表达：还活着。也就是勉强使自己的生活运转着，为了这种“运转”不得不背井离乡四处打工或者在自己那点土地上拼死拼活的刨食，这里面有多少艰辛和血泪已经不需要再重复，城市里的贫民或多或少还可以得到一定的国家救济和照顾，这个群体的数量也是很大的，一般的估计在三千万左右，这个统计数字小得真是太具“中国特色”了。我的家乡属于中国中上等水准的农村地区，我这里的农民生活还算过得去，楼房普及，但孩子缀学厉害，主要是因为学费太高，很多家庭觉得供不起读书更划不来，农民如果生大病多数去医院治疗几天之后一般也就出院了，撑一天算一天，环境污染很严重，癌症是普及了，等死而已。 <br />
<br />
从以上分析我可以认为：中国近十四亿人口中有近一亿人属于被遗忘的群体，这个世界与他们无关，或者说他们与这个世界无关。有一亿人基本不存在拥有财富的问题，也就是说“财富”这个概念不适用于他们，还有至少四亿人不存在“购买”能力，他们仅仅能保证糊口，商品生产者不必考虑他们的消费能力。 <br />
<br />
那么我对中国阶层的分类是这样的：<br />
<br />
第一个阶层（也是处于最顶端的王者阶层）是由几百个家族组成，他们拥有骇人听闻的财富，是这个国家的掌控者。<br />
<br />
在他们之下是第二个阶层――地方性的豪族，数量也许是几万家，这些人控制着地方的权力，自然也拥有无与伦比的财产。<br />
<br />
第三个阶层是由公务员，事业单位人员、国企管理人员、垄断国企人员和私营企业主等这些人中的佼佼者以及顶级白领阶层等这些群体中的人员组成。<br />
<br />
第四个阶层是生活比较安逸的一般民众，他们经济上还算比较宽裕，但是社会地位不高，对社会没有什么影响力。<br />
<br />
第五个阶层是由城市平民和农村中生活比较好的农民组成。第六个阶层是贫困群体，也就是四亿没有购买能力的民众。<br />
<br />
第七个阶层是一亿没有财富的赤贫阶层，<br />
<br />
第八个阶层就是最后那一亿灾难性赤贫的阶层。 <br />
<br />
中国的大部分财富掌握在属于前两个阶层的手里，有六亿人拥有的财富微乎其微，平（贫）民阶层拥有的财富不及第一第二个阶层所拥有的财富多，主要财富掌握在前三个阶层的手里。 <br />
