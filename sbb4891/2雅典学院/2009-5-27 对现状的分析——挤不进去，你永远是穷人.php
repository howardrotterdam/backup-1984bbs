第三节：国民经济是由权力阶层操纵的非正常经济<br />
<br />
 <br />
<br />
<br />
中国的经济组成笼统地说是由公有经济、外资和私有经济组成，但是所谓的公有私有经济形式已经完全变味，公有经济未必公有，私有经济未必私有，实际上皆是由特权阶层操纵着，它们是由特权阶层操纵的非正常经济，勉强可以称之为官僚权贵资本主义经济，因为它比通常所称的权贵资本主义经济更加特权化。 <br />
<br />
公有经济的最主要表现形式是垄断国有企业，这种垄断形式有两种，一是电信石油之类的在国内外仍然有一定竞争对象的企业，另一种是水电气之类的完全垄断的经营实体。<br />
<br />
有一段时期，公有经济实体里的职位还是一个鸡肋，而现在它们却是香饽饽，电信石油烟草之类靠着高度垄断和半执法者的地位以超烂的管理却能获取骇人听闻的利润，而水电气之类的经营管理者更是以狂赌烂嫖的管理方式照样获得无法形容的丰厚利益，即使它们年年巨额亏损，以至于曾经代表高尚体面的外资白领职业完全黯然失色，现在谁还好意思以外资白领职业为荣呢？ 其它国家也有中国这两种国有经济形式，一种是ZF认为需要国有的如核工业之类的企业，一种是供水供电之类的服务企业。但是这类国有经济是被压缩到最小范围内，更要接受公众的严格监控，总之，不能因为你的特殊性就骑到民众头上去，而中国这些公有经济实体却是实实在在地骑到民众头上作威作福了。 <br />
<br />
公有经济实体为何能如此？是因为公有经济实际上已经完全成为官僚权力集团的私家后院，只不过挂着公有名义的羊头而已，如果说普通老百姓做公务员的机会已经很小，那么你想进入待遇极其丰厚工作极其轻松的垄断公有经济实体成为一个“正式”的员工则是更难（请注意“正式”这个词，下文将会解释），因为这个公有实体已经基本上对老百姓关上大门。 国有企业的真实面目是拥有国有企业特殊权力的私有企业，通常称为官僚权贵资本，我们的还要加上有中国特色的权力，有了这种权力的官僚资本经济实体对于其拥有者来说等于有了一台光速印钞机。 <br />
<br />
当今国有经济实体还有一个原罪，那就是残酷剥削“合同工”，如果你能做一个光荣的垄断国企正式员工，那你发达了，你可以躺在那里享受惊人的丰厚待遇，如果你只是公有经济实体里的临时工，那么你只是一头干活的驴子，从电信烟草到水电气交通等等各类公有企业都招募了巨量的临时工人，给予其和正式工相比微不足道的薪水和待遇，可以说，国有垄断企业之所以有那么高的利润，也和雇佣了很多只需要给予微薄待遇的合同工有很大的关系，如果没有临时工合同工，水电气交通等ZF经营实体简直难以想象还要亏损到什么程度，是国有经济实体中“非正式工”的汗水把正式工的钱包浇灌的无比丰厚，真是同处一片天地却是冰火两重天，这是令人发指的剥削，是完全违反人类基本道德准则的行为。<br />
<br />
私有制的蓬勃发展极大的冲击了这个社会的一潭死水，然而对于这种极具中国特色的私有经济是要打个大大的问号的，因为中国这种私有制有别于通常所称的私有制，它的主要形式仍然是官僚资本，由特权阶层控制的私有经济占据了私有区域的最主要部分。 特权阶层通过掌握国家权力而获取自己经营上的便利，如每一个国有经济实体的背后都有很多特权阶层拥有的私有企业为其“配套”服务，这种“服务”的利润自然是很高的，这是转移国家资产的一种微妙方式，掌握国家权利的阶层将ZF工程项目交给自己阶层经营的实体承做，这块区域是今天中国暴发户产生的主要源泉之一。而各个ZF衙门利用自己区域范围的管理权力垄断了这些区域的产业经营权，不容民众染指或者极力打压普通民众的正当经营，至于那些“纯粹”的草根私有经济实体则时刻面临ZF各部门的“法规”威胁，如环境保护，税收，劳动保障，不规范的法律条款随时可以要你的命，迫使你乖乖的卷缩在特权阶层的淫威下苟且生存。 <br />
<br />
外资尤其是西方资本的进入可以说极大的推动了中国的进步，但是对中国的负面作用也越来越明显，一是环境污染的转移，二是西方面对利益的诱惑丧失了他们的道德标准而纵容黑暗的肆虐。我这里就有一个很大的外资项目----扬子―巴斯夫化工项目，它的污染是显而易见的，而其配套企业的污染更是惊人，但是扬巴这个项目如果建在德国恐怕就不是这个样子了，今天中国的市场对西方确实有很强的诱惑力，甚至可以用来要胁西方，资本家的根本目标就是利润，这种诱惑迫使西方人经常放弃他们的道德和价值观。 <br />
<br />
这里提一下我国一种特有的经济形式，就是ZF建筑里的楼台馆所，大家应该注意到，在中国，屁大的ZF机构都要建个大楼，虽然屁大的官员都拥有面积超大装修超豪华的办公场所，但是在如此情况下各个ZF大楼仍然有巨量多余的空间，这个空间自然就用来经营了，或出租或自营，大家不要小看这个区域范围内的经济力量，再加上那些数量极其惊人，由ZF部门、社会事业部门及军、警等所拥有的商业建筑，这些是ZF利益集团的很可观的一笔“额外收入”。 <br />
