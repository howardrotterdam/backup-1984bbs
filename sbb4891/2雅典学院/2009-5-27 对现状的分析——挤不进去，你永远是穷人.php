第八节：人格的扭曲，民族的沉沦 一个民族不会因为贫穷落后而灭亡，但是民族失去文化和精神后注定要灭亡。 “谎言重复一千遍可以成为真理”，在一个“全控型”社会里，说真话者注定要从肉体上或精神上被消灭，整个社会运行中除了谎言就是谎言。<br />
<br />
无数遍谎言后，甚至连奴才般的民众也会不自觉地认为掌权者的统治是理所当然的。 长期以往，这种国家的国民自觉不自觉的便把谎言当成真理，把真话当成“病态”，而在“真理”和现实的极度扭曲中，这个社会的民众并产生一种“扭曲现象”：他们一方面认定周围一切官员都是贪官污吏，另一方面却又相信某些伟人或远处的高层人物是清廉高尚的，所谓“皇帝是好的，都是下面官员贪腐”。他们一方面无比羡慕西方，崇拜西方的物质和文化，并以自己的子女家属能够去西方为荣，另一方面却又在现实中极端仇视西方，不惜对那些公开宣称欣赏西方的同胞以最恶毒的攻击和伤害。他们一方面了解这个社会的全方位阴暗，另一方面又强烈抵触西方对中国的批评。这是中国人人格扭曲的三个最普遍特征。 在这种“真理”和现实的极度扭曲中，中国人，所有阶层里的大部分人，都为自己编织了一个安慰自己灵魂的理论：西方不也有腐败吗，克林顿不也搞女人吗。这种以西方社会腐败的个案来为中国体制辩解的理论，是国人的精神鸦片。<br />
<br />
在无尽的谎言和扭曲的人格下，权贵们一边蹂躏这片土地，一边又高举爱国主义大旗来当作自己的遮羞布，而民众还真的不自觉地响应这种无耻之极的喧嚣，但是民众的“口号爱国”激情却又是建立在“让别人流血流泪”的基础上的，他们会被这种喧嚣激动得热泪盈眶，却又不愿为此受到哪怕一点点的损失。这种扭曲的人格倒是有历史传统的，大家看看明朝那些生吞袁崇焕肉块的民众就明白了。<br />
<br />
这个国家另一个空前绝后的扭曲现象――数亿赤贫民众和无数的摩天大楼形成强烈的对比。壮观的城市高楼群成为大部分国人阿Q式骄傲的麻醉剂，那些高楼大厦有那一座地基不是用民众的血泪堆砌起来的？那些以中国城市的“壮观”为荣的人，他们愿意成为“地基”吗？而对于大部分国人来说，那些高楼大厦与他们又有什么关系？这种漠视别人的生命和痛苦，和让别人去流血的“口号爱国”异曲同工。</div>

							
							
							
							
