																											<div id="postmessage_338480" class="t_msgfont">其实，我算幸运的了，学生时代没有系统学过《论语》，免受浸淫，教科书上只有五六篇。什么“有朋自远方来，不亦说乎”之类，为了考试也随便背过。现在想想，操你妈孔老二，竟敢拿这种婴儿觉悟糊弄我们几千年。<br />
<br />
上大学后，看到于丹之流借国学的幌子，狂赚跟风赶时髦的无知屁民的钞票时，我决定自己拿本《论语》好好看看。也许我天生反骨吧，实在吃弗消这个可怜的糟老头的说教。任何人都可以假装成道德仁义之士，口号满天飞，把理想当作现实，吸引不明真相的群众。况且，表演、作戏可是虚伪中国人的拿手绝活啊。<br />
<br />
现在，已经很清楚了，贵国低劣文化的核心毒素就是儒家的那一套虚伪言说。经过2000年愣头书呆的哼吟传诵，孔丘的无知妄言已深入汉人骨髓。<br />
<br />
我不明白从明朝的李贽开始，批判儒教就已经逐渐系统规范、符合逻辑，到五四时代集大成，又经过毛腊肉的破四旧、批林批孔，照理说儒教这东西应该彻底被抛弃，为何如今又死灰复燃？这帮13亿蠢货是怎么想的？<br />
<br />
&nbsp; &nbsp; “君君臣臣父父子子”导致汉人奴性泛滥，衍生出各种纲常教条，压制人性、懦弱、胆小、装孙子成为汉人必修课。<br />
<br />
&nbsp; &nbsp; “不孝有三，无后为大”导致汉人重男轻女，人口泛滥，人们掠夺资源，各种人为灾害频发。<br />
<br />
&nbsp; &nbsp; “子不语怪力乱神”压制宗教，又拿不出恰当的填充物，导致汉人普遍心灵空虚，精神阳痿，一个个苦大仇深，宽容成为奢侈物。<br />
<br />
<br />
翻一遍《论语》，君子小人满天飞，他妈，做君子就那么容易吗？不按你说的做就是小人吗？<br />
<br />
孔丘就像一个过了更年期的老女人一样，占着歪理，唯我独尊，其他人在他眼里一钱不值。自私狂妄，猥琐装逼，让这个落魄文人有了最后的精神乐园。<br />
<br />
中国人真是可悲，唯一能拿得出手的所谓顶级文化遗产就是这本玩文字游戏的《论语》，目前又盲目推广孔子学院，妄图把这种劣质教条兜售全球，真是愚不可及。</div>

							
							
							
							
