																											<div id="postmessage_339896" class="t_msgfont">政治认同与文化认同的出路何在？上述思潮并非单纯地源于对政府谄媚的动机，它还反映了中国在追求现代国家认同的漫漫长路上的一种精神迷失。中华认同建立在肯定中华文明优越性的基础之上。随着清军在西方国家的“坚船利炮”前之惨败，中国士大夫们的文化天下主义的深层自信从此灰飞烟灭，并因此引发了认同危机。这一危机以极其痛苦的方式为那个时代的知识分子所领略，且一直持续至今。从19世纪中叶到1949年人民共和国成立，关于这种认同危机的争论从未停止。现实中寻找民族认同由两个不可分割的方面组成：一方面，通过再造传统，重新定义文化认同；另一方面，诉诸从改革到革命等各种手段，建立民族国家。共产政权的建立一度凝聚了民族对国家认同的追求，民族主义在毛泽东建立的社会主义国家那里找到了一个安身立命的避难所。从此，中国除了共产主义意识形态之外，其他任何思潮都被赶尽杀绝。中国文化的优越性被共产主义优越性所取代，传统的天下主义改头换面为共产主义。邓小平的开放政策打开了这个封闭的世界。文化认同危机通过文化的自我肯定和重新检视中西关系两种追索形式再次表现出来。当年，西方社会在工业革命到来时通过打破沉重的宗教枷锁为世界祛魅。而今日的中国却面临着双重祛魅。第一重祛魅是去除共产主义之魅。共产主义在中国成功的深层原因是几千年来中国农民对平等的梦想；然而，30年的封闭终于激起了追求自由和开放的欲望，共产主义优越的信念冰消雪散，乌托邦之梦从此破灭，民族主义的归属避难所也化为乌有。第二重祛魅是“理想西方”神话的坍塌。从1979年到1989年，“理想化的西方”俨然如同否定中国无产阶级专政现实的镜子的另一面。可是随着开放和距离的拉近，西方国家的政治现实主义与维护人权话语之间的矛盾，以及意识形态意义上的民主原则与西方社会复杂现实之间的反差摧毁了西方榜样的形象。假如说对共产主义的祛魅引起了民族精神的复兴，那么，“理想西方”的幻灭则在中国引发了与一个平庸的西方世界竞争的愿望。双重祛魅引入了对自身与他者的怀疑，导致了双重认同危机──政治与文化认同危机。此双重危机不仅深重，而且二者之间还存在着一种内部张力。共产主义的幻灭导致了平等、公正等价值认同的消失；政权对传统价值力量的诉求又使人们重新感到了精神象征的缺失。近代中国对儒学价值的指责是因为这一价值系统无力保卫中华文化，也无法为文化中国提供归属指引。而当前向传统的回归无疑凸显了政治的无能，因而更加强化了民族对政治认同的需要。随着极权主义意识形态的淡出，人们重新发现长期以来以为已经被共产主义超越的西方带着其文化差异、经济强权、战略傲慢和政治体系再次卷土重来。在正常情况下，否定共产主义意识形态应该伴随着价值系统的更新；然而，中国思想界解构意识形态的正面效应一方面为“理想西方”的幻灭而抵消，另一方面也被全能国家的暴力所屏蔽。从这一角度看，今日中国的认同问题和近代以来的认同危机一样，政治认同不足大于文化认同不足。无论是近代还是今天，只要中国文化还无法支撑起一个能够以共同的价值认同凝聚国人的政治实体，中华民族的认同危机就会周期性发作并引发政治危机。从本质上看，现代中国的民族认同是“我应该是谁”的一种开放并指向将来的认同追寻，而不是严格意义上的“我是谁”的身份危机。</div>

							
							
							
							
