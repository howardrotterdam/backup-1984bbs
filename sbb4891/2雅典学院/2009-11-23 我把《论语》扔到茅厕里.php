我留意了一下一些儒教分子的言论，特别是台湾一个被称为“国学大师”的人写的鼓吹儒教的书。且不说他书里面那些可笑的常识性错误，如已经被很多专家如张中行指出的作者对“无友不如己者”“不如诸夏之亡也”等含义的解释完全是不顾常识按照自己的需要蓄意曲解篡改，甚至连通假的常识都不顾了。但说以此为代表的儒教分子的那些戏说历史的弱智言论，完全是在故意误导没有古文根基，没读过原始典籍的读者。<br />
<br />
这一类摩登儒教，虽然经他们胡乱引申发挥，跟他们祖师爷的原始意义已经有所出入，但是本质还是差不多的，而且因为这些言论是很有代表性的，是现阶段儒教分子极力宣扬鼓吹并且在中国很有市场的一类言论（他们甚至还大肆鼓动所谓的“读经运动”，实质上早就堕落成商人牟利的做秀表演），所以我们下文就以此来看看。<br />
<br />
虽然现在的儒教分子已经不好意思再提什么“尊王”“忠君”之类的垃圾了，而是改口宣扬什么“孝悌”才是儒家的本义，妄图以此迷惑世人，博取人心，不过稍有历史常识的人都知道，这只是他们为其祖师爷遮丑的无耻谰言。<br />
<br />
《论语》一开篇，学而第二，有子就说：“其为人也孝弟，而好犯上者，鲜矣；不好犯上，而好作乱者，未之有也。君子务本，本立而道生。孝弟也者，其为仁之本与！”<br />
<br />
这段话清清楚楚地将儒家所要宣扬的“孝悌”到底是为了什么目的表露了出来，宣扬“孝悌”从来不是什么儒家的“本义”，只是手段，维护君尊臣卑统治秩序的权宜之术而已。<br />
<br />
可惜即使是这样，他这话也说得太早了一点，古代实行科举制之前，普通人要做官有一个很重要的途径，就是“举孝廉”，曹操就是一个典型的代表，他的为人倒也确实是很“孝悌”，为报父仇，发兵攻打陶谦，一路屠城，残杀了数以千万计的百姓，这就是儒家所推崇的“为仁之本”吧。至于犯上作乱，那他更是“不鲜矣”。这样“孝悌”的人物在中国历史上数不胜数。<br />
<br />
篡汉的王莽，也是一个著名的“大孝子”。<br />
<br />
实际上，一个人爱不爱父母跟他爱不爱邻居，人民，国家，根本没有任何逻辑关系，相反，一个人倒是有极大可能性为了狭隘的“孝”作出损公肥私，伤害他人的事情，这样的例子在中国历史上还少吗？<br />
<br />
以孔子自己的言行为例：《论语》子路篇第18，叶公语孔子曰：“吾党有直躬者：其父攘（注：偷盗）羊而子证之。”孔子曰：“吾党之直者异於是：父为子隐，子为父隐，直在其中矣。”<br />
<br />
说老实话，如果我的亲人做了错事或者犯了法，我也是会为其隐瞒包庇的，我相信绝大多数人也是会这么做的，人之常情嘛，不过我还能意识到我这么做是错误的，而且违法，如果没被抓住就算了，抓住了就认栽。<br />
<br />
可孔子就能够厚颜无耻颠倒黑白地说“直在其中矣”，你要干坏事你就干嘛，你还硬要强词夺理把你干坏事说成是“正义的”，自此之后，就开创了中国人一种很恶劣的传统：即，到底什么是正义，真理，并不重要，重要的是人们可以随心所欲地将自己想要达到的目的解释成是“正义”“真理”。<br />
<br />
所以说儒家文化虚伪得令人发指。<br />
<br />
儒家宣扬的“孝悌”是很有迷惑性的，表面上看，爱父母应该是一种很高尚的道德，也是人类的普遍道德原则，可是儒家志不在此，他们所宣扬“孝悌”只是作为维护君尊臣卑等级秩序的手段，权宜之术而已。<br />
<br />
即使这样，儒家文化中的“孝道”比起西方文化中的“博爱”境界也是有天壤之距。<br />
<br />
人生三大感情，亲情友情爱情，我们先讲亲情，因为这是人伦之首。<br />
<br />
人所共知的是，母爱父爱是世上最伟大最崇高最纯洁的感情，在这一点上，中国人的境界是远远不如西方人的。<br />
<br />
西方人的母爱父爱是完全不图回报的爱，无私的爱，不象中国人那样隐含势利，当作一种人生的感情投资。<br />
<br />
中国人拼命地生育，而且重生男不生女，就是作为一种养老的投资。所以中国人对于不是自己亲生的子女，往往比对待亲生子女感情淡薄，疏远，而且极力防止养子女知道自己的真实身世，因为他觉得这种投资不保险；但是西方人收养别人的子女，却全心全意地当作自己的亲生子女倾心疼爱，无私奉献。哪怕是异族的孩子。<br />
<br />
从东西方历史传统就很容易看出这种差异：中国人的皇位是一定要传给自己亲生儿子的，不然的话，就是被视为变了天，失了权，要动刀流血的；但是古罗马帝国的皇位一般是皇帝在有能力的人中选好继承者后，收其为养子，以此名义传位于后者。很少有传给亲生儿子的。从这种历史传统的差异就可以看出东西方道德境界的差异。<br />
<br />
以前发生的那件美国白人跟中国移民争夺子女的著名案子，就很说明问题，中国人没钱的时候就把亲生子女送给别人（甚至卖给别人）抚养，什么时候觉得需要延续这笔投资了又想去抢回来。中国人很难理解西方人为什么费力不讨好地去收养其他种族的孩子，而且人家还不怕养子女长大了以后知道自己的亲生父母是谁，在哪里。他们不担心养子女背弃自己去找亲生父母，因为人家完全是无私的付出，是真爱，不像绝大多数中国人完全是把生养子女当作是一种养老的投资。（注意：我说的是绝大多数，已经将少数良知未泯的包括我自动排除在外了）<br />
<br />
中国的重男轻女思想极为严重，为什么？因为生男是一种合算的投资，生女是一件赔本的生意，生女不能给自己养老送终，是没有价值的投资。中国的农村，抛弃甚至杀死初生的女婴的情况非常普遍，我在农村时，就经常看到有人将生下来的女婴悄悄杀死埋掉。虎毒尚不食子，在爱这个领域，大部分中国人的道德水准甚至连禽兽都不如。<br />
<br />
中国人今天稍微变得象点人样了，完全源于接受了西方文明的天赋人权，博爱，民主，自由，平等，等思想观念的启蒙，教育。中国人应该永远牢记正是因为有了西方这个恩师的教诲，才有了中国人现在初脱兽形的人样社会。<br />
<br />
西方人的爱是真正做到了“为爱而爱”，正是因为西方人的父爱母爱是完全出于真爱，不图回报的爱，所以相应的，西方人的孝敬父母，也是真正做到了基于真爱的一种感情。而中国人由于其一开始的父爱母爱就是出于一种隐含势利的养老投资，所以其衍生出来的“孝道”本质上也只是一种还债性质的行为。<br />
<br />
表面上看，西方的父母子女之间关系好像很疏远，冷淡，以彻底的科学化，制度化，法律化，将人的权利和义务分得清清楚楚，好像有点冷冰冰，不够温馨，不够人情味，但是实际上，只有这样才能够使人的基本幸福得到切实的保障。<br />
<br />
虽然西方人不像中国人那样搞什么形式主义的“孝道”，非要勉为其难地让子女与父母甚至祖孙几代人挤在一起凑合着磨摩擦擦地过日子，但是西方人通过完善的养老医疗等福利制度将老有所养，老有所依问题解决得很好。并且西方人父母子女之间并不缺乏温情和关爱，在各自独立保持自己生活方式的情况下，很好地做到了有礼，有节，彼此尊重，合乎人性地感情交流。<br />
<br />
西方人对待子女从小就作为一个有独立人格有尊严的人平等尊重对待（不像中国人把子女当作畜养的小猫小狗一样随意打骂），并真心疼爱，全心全意地为他的前途着想，培养好他的生存能力，长大以后则尊重其意愿任其自由发展，不图回报；而子女长大以后，就离开父母独立生活，并时常保持感情的联络，也认真履行经济上赡养父母的责任（这并不矛盾，因为他们有完善的养老医疗保险制度，并不需要依赖子女）。<br />
<br />
而没有父母的孤儿，西方人也通过制度性的孤儿院收养使其免于象中国人那样流落街头，或者被卖来卖去的悲惨命运。<br />
<br />
通过制度建设，西方人真正地做到了“老吾老及人之老，幼吾幼及人之幼”的理想境界。<br />
<br />
而中国人虽然自以为是地强调并吹嘘所谓的“孝道”，但是中国社会的老有所养，老有所依问题从来就没有得到真正解决过，特别是在人没有能够有机会作出生养子女这种投资的情况下。<br />
<br />
中国的城乡中大街小巷，到处都可以看到靠拣垃圾为生的老年人，可以说，中国人才是世界上最没有孝心的民族。<br />
<br />
中国人所谓的“孝道”是非常虚假的，是一种还债性质的行为，当然，能够知恩图报也是一种美德，但是中国人由于背负着这种还债的心理包袱，尽管家庭内部如婆媳关系紧张恶劣，也要碍着面子勉强凑合着过，再加上彼此生活习惯的差异冲突，长期这样压抑的生活，就容易产生一种变态心理，导致中国人在表面“孝顺”父母的形式下，变相地虐待父母，如把父母当作是带小孩做家务的免费保姆，以求得心理平衡；或者是在父母在世时对其刻薄寡恩，父母死了之后又来隆重地“厚葬”，以此形式主义标榜自己“孝顺”。<br />
<br />
这是中国人在中国文化这个污浊环境中难以摆脱的悲哀。<br />
<br />
其实这个问题并不是只有中国才有，很多第三世界国家，如印度，也有这种“大家庭”的传统，因为这是一种农业社会遗留下来的共有传统，但是别的国家民族都没有发展到中国这样变态的“孝道”。<br />
<br />
中国古代，子女对于父母是有一种人身依附关系的，父母对于子女有绝对的支配权力，可以生杀予夺，如同君对臣，“父要子亡，子不得不亡”是有真实的刑律制度支持的，父母如果认为子女不孝，可以不需要任何证据，向官府控告，就直接定罪，处死（一般都是陵迟），在这样残酷的刑律支持下，中国古代当然“孝子”多多，这就是中国文化所谓“孝道”的真实本相。<br />
<br />
儒家文化的虚伪性还典型地表现在《论语》学而第十一章：子曰：“父在观其志，父没观其行，三年无改于父之道，可谓孝矣。”（三年在这里是表示“多年”的意思，但后世守孝则确以三年为期）<br />
<br />
为什么父在观其志？因为古时候父亲在世时，儿子不能自主行事，要按照父亲的意志行事，要百依百顺。这倒也罢了，可为什么父亲去世了，仍然要“三年无改于父之道”？这不是发神经吗？你爹都死了，你再假惺惺地“无改于父之道”，装出一副“孝顺”的样子，还能对他有何益处？<br />
<br />
再说了，你要“孝顺”，要“无改于父之道”，那就应该一辈子“无改于父之道”嘛，做人要有始有终，为什么三年（或X年）就不了了之？这到底是出于“孝顺”，还是做秀，还是怕坐牢，还是怕鬼魂缠身？<br />
<br />
而孔子自己又是怎么做的呢？孔子的母亲刚死不久，他还在守孝期间，听说季氏举办宴会，就不顾自己还在守丧的身份，急忙赶去蹭饭吃，结果被季氏家臣阳虎赶了出来。<br />
<br />
所以说儒家文化虚伪得令人发指。<br />
<br />
儒家所提倡的所谓“孝道”，“以孝治天下”，在现代社会中根本就不具有可操作性。硬要强制性地搞什么形式主义的“孝道”反倒造成父母子女之间的感情隔阂。<br />
<br />
孔子说：“道之以政，齐之以刑，民免而无耻；道之以德，齐之以礼，有耻且格。”――――这是大错特错！人的道德不是天生而来的，正是依靠法制约束才能建立起人的道德观，羞耻感，而儒家将道德说教的重要性置于制度建设之上，从一开始就犯了一个根本性的错误。后来发现行不通时，又开始用严刑峻法来弥补，这又错上加错，因为要做到真正的社会公正和谐，法律合理，不是依靠严刑峻法，而是需要三权分立的制衡，协调。这是以儒家为代表的中国文化的致命缺陷。<br />
<br />
现在中国是世界上刑法最严酷的国家之一，但同时也是世界上社会道德最败坏社会治安最恶劣的国家。其根源之一就在于这种文化传统。<br />
<br />
还有，儒家宣扬的这个“仁”的理念，跟我们通常理解的“仁爱”有很大出入，确切地说，儒家的“仁”这个概念，指的是一种道德规范，而这个道德规范是要以符合君为臣纲的统治秩序来衡量的，所以有的时候，“仁”的理念与“仁爱”有相重合之处，因为便于维持统治秩序；但更多的时候，“仁”则与“仁爱”毫不相干，甚至完全相反。<br />
<br />
比如说，“仁”最高的标准就是不能“越礼”，也就是说诸侯使用的祭祀礼仪规格以及行政待遇不能超过天子，以此类推，社会中不同等级的人都不能有超过上级阶层的待遇规格。<br />
<br />
孔子的学生颜回死了，无钱安葬，颜回的父亲请求孔子把自己的车卖了给自己换棺材安葬儿子，孔子说：卖了车我就只能走路了，而我是曾经做过官的人（曾居大夫），照礼不可以步行的，所以这事我帮不了你。这就是孔子所谓的“仁”和“礼”。<br />
<br />
《论语》八佾篇第一，孔子谓季氏，“八佾舞於庭，是可忍也，孰不可忍也？”，仅仅因为人家多安排了几个祭祀的舞者，他就暴跳如雷到这种程度，但是从没看见他对在残酷的奴役压迫和诸侯战争中的民众苦难表现出这样的愤怒过。由此就可以看出孔子心目中的“仁”到底是个什么东西。<br />
<br />
孔子自己就坦承：仁就是“克己复礼”。<br />
<br />
儒家所说的“仁者爱人”，这个“人”，是有特定意义的，也就是说是要符合统治要求才能算是“人”，用孟子的话来说，就是：顺之者人也，逆之者禽兽也。你不听他的话，他就不“爱”你了，甚至你连人都不算了，他可以动手杀你。<br />
<br />
孔子跟随鲁定公去与齐国会谈时，仅仅因为对方奏乐时跳的舞不符合他认定的“礼”，就硬逼着齐景公砍掉了舞蹈者的手脚。（热爱崇拜儒家文化的叶公好龙者注意了：我知道你们很多是喜欢跳街舞，迪斯科的爱国小愤青，如果你们回到你们梦想生活的孔子时代，你们很有可能会被砍掉手脚的）<br />
<br />
孔子任鲁国大司寇时，因为大夫少正卯聚众讲学，把孔子的门徒都吸引过去了，使得“孔子之门三盈三虚”，恼羞成怒的孔子就找了几个莫须有的罪名把少正卯杀了。<br />
<br />
所以说儒家文化虚伪得令人发指。<br />
<br />
儒家文化的所谓“礼”，根本不是我们现代意义上的“文明礼貌”“平等待人”的意思，而是专指一整套君尊臣卑的等级秩序的意思，一种君臣名分，纪纲。<br />
<br />
孔子说：“巧言、令色、足恭，左丘明耻之，丘亦耻之。”，可是从他自己的言行来看，他又何尝不是“巧言、令色、足恭”。<br />
<br />
《论语》乡党篇：【孔子於乡党，恂恂如也，似不能言者。其在宗庙朝廷，便便言；唯谨尔。朝，与下大夫言，侃侃如也；与上大夫言，（门言）（门言）如也。君在，（足叔）（足昔）如也，与与如也。君召使摈，色勃如也。足（足具）如也，揖所与立，左右手，衣前後，檐如也。趋进，翼如也。宾退，必复命，曰：“宾不顾矣。”入公门，鞠躬如也，如不容。立不中门，行不履阈。过位，色勃如也，足（足具）如也，其言似不足者。摄齐升堂，鞠躬如也，屏气似不息者。出，降一等，逞颜色，怡怡如也；没阶，趋进，翼如也；复其位，（足叔）（足昔）如也。执圭，鞠躬如也；如不胜。上如揖，下如授，勃如战色，足（足宿）（足宿）如有循。享礼，有容色；私觌，愉愉如也。】<br />
<br />
