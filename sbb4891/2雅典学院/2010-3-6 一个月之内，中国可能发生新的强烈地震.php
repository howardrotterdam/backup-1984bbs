													<h2>一个月之内，中国可能发生新的强烈地震</h2>
						
						
																											<div id="postmessage_505462" class="t_msgfont">一个月之内，中国可能发生新的强烈地震 <br />
&nbsp; &nbsp;　　这是一篇我首先提出“地球质量重新布局”理论的文章<br />
　　<br />
　　笔者声明：本来工作很忙，明天学校就上班了，并没有想写这样一篇文章。但是一个接着一个的事实印证了我的理论，就在台湾高雄发生地震前的一个小时，在我送爱人上班的路上，我对我的爱人说：也许是一个月，也许是更快的时间之内，我们中国东海的某个区域会发生强烈的地震。所以本文意在从科学的角度上来分析地球内部运动，除此之外，别无它意。如有不同观点，欢迎批评和指教。<br />
　　<br />
　　我们大部分人都知道，我们脚下的这个星球是由多层物质组成的，并不是从地表到地心都是泥土，我写这篇文章不为科普、不为像韩寒一样耍笔杆，因为这不是散文。我写这篇文章不为获奖，因为这也不是论文。笔者意在阐述自己的观点，希望读者在遇到关键问题时能查阅相关资料，而不要继续对我提出新的、无需我解答的问题。至于本文中有些与传统地质科学相冲突之处，我还是希望地质学者们能够认真阅读我的理论，因为我是一个十岁就无师自创阿基米德原理的人，一直到我初中时才知道这种理论有人已经发现过了…<br />
　　<br />
　　首先我们要从地球的内部构造上来分析，根据已经探明的资料证实，地球分为地表、软流层、地幔、外核、内核等，当然也会有其他的区分方法，但是采用这样的分层方法更有利于本文研究现有的自然现象。<br />
　　<br />
　　我们暂且放过地核不研究，来看看软流层，笔者通过查找资料得出这样的定义：“软流层(Asthenosphere)又叫软流圈，位于上地幔上部岩石圈(Lithosohere)之下，深度在50-250km之间，是一个基本上呈全球性分布的地内圈层。软流层一般认为是岩浆的主要发源地。软流层的分布具有明显的区域性差异，总的规律是大洋之下位置较高（一般在60km以下），大陆之下位置较深（深度在120km以下）。软流层顶底界面不十分确定，与岩石圈之间无明显界面，具有逐渐过渡的特点。”软流层的形成是需要一定条件的，学过物理的人会知道，它是在高温、高压的条件下将我们见到的物质液化之后形成的，在这里我们需要注意，软流层是液体！那么，高温高压是怎么来的呢？答案应该是“万有引力”从宇宙中来看，地球其实就是一团以地心为中心的物质集合。星云通过长年累月的积累，物质密度变得很高，质量很大，就导致星云的塌陷，形成比星云密度更高的星球。星球再吸引一些从他旁边经过的物质，久而久之物质团变得大了，星球表部的物质对星球内部的物质形成了很强的挤压作用，然后就形成了高压区域，高压区域应该分为固态和液态，固态是最高压的位置，很多的物质粒子被紧紧压缩，而过度的区域中就会是液态的，他们通常不会受到太过强烈的压缩，于是高压导致了高温、高温导致了液化。就这样，地球从一团星云变成了现在的分层星球。<br />
　　<br />
　　我们再来看看地球的自转，开玩笑的说，如果地球不自转的话，可能体积会比现在小很多，但是地球的自传是怎么来的呢？答案很简单：是多年累积下来的动量作用，但本文重点不是研究它为什么会转，而是研究它在转的过程中会怎样。<br />
　　<br />
　　地球转了，就要沿着一定的轨道，否则就成活物了，抛开公转不去研究，在地球自传过程中有一个轴心，我们通常称之为“地轴”，地轴的北端在北极、南端在南极，地轴在地球中的位置并不固定，而有微小的移动，造成“极移”。极移的成因有很多因素，但笔者觉得除地球自古以来的规律性极移之外，主要原因是地球这团物质中的某些位置会出现质量的变化。这是有科学依据的，那就是我们之前谈到的“动量”，也就是“惯性”。然后我们再在现实生活中寻找一个例子，笔者很容易的联想到了东北人玩的“尜”，也有人称之为“陀螺”。这种玩具的旋转类似于地球的自转，如果陀螺的密度是均匀的，它就会转的很稳定，而地球的密度是不会均匀的，虽然经历多年的沧桑变化，地球已经找到了稳定自转的那一个轴心，但是这样的稳定也只是暂时的，因为地球上的物质在移动！我们希望地球上面的物质移动相对地球本身的质量要尽量的小，否则我们可以想象在陀螺的某一个位置钉个钉子的效果，那就是地球的结果。<br />
　　<br />
　　说了这么多，有的读者可能觉得笔者正在科普，其实不然，笔者所讲的这些都是紧紧关连着的，现在就有那么一根钉子已经开始钉在陀螺上了。<br />
　　<br />
　　那就是地球两极的冰川！<br />
　　<br />
　　笔者查找相关资料发现地球的总质量大概是6x10的24次方千克，南极冰川的质量是3x10的19次方千克，而北极冰川的质量没有相关的资料，很可能是已经融化殆尽了。如果南北极冰川全部融化了，二十万分之一的质量将重新布局在这个星球，这是灾难性的！也许有的人会觉得“不就是海平面升高了么？大不了我们向内陆迁移啊!”，其实这不只是海平面升高的问题。我们现在的地表有海洋、有陆地，而以地球的轴心为准线，我们可以发现海洋和陆地的整体质量是平衡的：位于地表、在地轴两侧的陆地和海洋的质量大致不会相差很多。如果南北极冰川全部融化的话，这些水就会流入海洋，而占地表总面积71%的海洋并不是均匀分布于地表的，他们质量会增加，面积会扩大，这将导致陆地、海洋相对轴心质量的严重失衡，地轴偏转在所难免，甚至可能会影响到地球的公转轨道。损失无法评估，后果也不可想象！（你知道陀螺上面多个钉子之后，陀螺会转到什么地方去吗？）<br />
　　<br />
　　也许2012，根本不需要外星物体的撞击……也许有的朋友觉得笔者在危言耸听，那么笔者用这样“质量重新布局”的原理来整理和预言一下我们地球上的运动吧：<br />
　　1）10多年前我们关注到地球南北极的冰川正在融化；<br />
　　2）我们可以观察到太平洋、大西洋海平面升高；<br />
　　3）地轴发生微小的、非常规的变化；<br />
　　4）受到新流入水的压力，漂浮在软流层的太平洋板块、印度洋板块、美洲板块东部、非洲板块南部微小的、缓慢的下沉；<br />
　　5）受到地球内部压力的影响，漂浮在软流层的亚欧板块、美洲板块西南部、南极洲板块、非洲板块北部微小的、缓慢的抬升；<br />
　　6）在此次地质运动过程中影响最大的莫过于两个板块间相互运动最强烈的地区，它们包括：东南亚、青藏高原、日本台湾一线、南美洲西海岸；<br />
　　7）除板块间的纵向运动之外，板块间也会出现常见的水平运动，会影响更广泛的地区。<br />
　　<br />
　　上述地质运动是一个不会很漫长的历程，运动速度视冰川融化速度而定，现在我们重点分析和预测一下我们近几年经历过的和即将经历的地震：<br />
　　1） 印尼海啸代表了印度洋板块下沉的开始，随后处于支撑点的汶川发生地震，接下来会是不丹-尼泊尔-伊斯兰堡一线发生更强烈的地震；<br />
　　2） 亚欧板块上升的明显标志是喜马拉雅山的加速成长；<br />
