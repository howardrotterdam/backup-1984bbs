													<h2>关于五毛与脑残之“一样论”</h2>
						
						
																											<div id="postmessage_666810" class="t_msgfont">五毛这东西，本义乃为网评员，广义就广了，泛指一切拿党国工资为党国说话和不拿党国工资也为党国说话的家伙。今天我们这里说的是狭义的五毛。<br />
五毛这东西，大家都知道是真的存在的，我们亲爱的党国还光明正大地把五毛培训照片发到媒体上来了咧。但是这东西虽然大家都知道他存在，但是却从来没有见过有五毛承认自己是五毛（或许有的，有的人故意通过“反讽”的手段来“自嘲”自己是五毛，来让人以为自己不是五毛。不过就算真有，我也不知道谁是这样谁不是这样，我也不是网警或者安安宝宝，我可没有技术力量去监控你的小电脑哦），所以五毛这东西就是一个“公开的秘密”，宏观上来说它是公开的――你我都承认有这码事，但是微观上来说它又是秘密――你有什么完全确凿百分之一百地说这个人或者那个人是五毛呢？（但是，人的眼睛虽然不总是雪亮的，但是还不是完全瞎的，有时候还是会正确地指出某人是五毛，虽然指出者和旁观者都没有办法证明他的话是真的。也正因为如此，所以五毛也可以光明正大地说我是五毛而不怕被拆穿，哪怕被拆穿了，只要死不承认，也没事。）<br />
<br />
<br />
<br />
五毛这玩意儿，说话（也就是他们的工作）的方式有很多种。有的时候他们是民族主义者，有的时候他们是毛左，有的时候他们是爱国青中老年。这些时候，我们都还好猜度。但是有的时候，他们也会利用敌人的武器来武装自己，例如伪装成自由控、平等控云云。因为这样一来，他们看上去就会和我们这些家伙貌似很像，所以我们往往也很难分辨他们究竟是什么敌是友了。举个例子，如果我是个五毛，我可以针对我前面的那两句话说：“你一上来就说什么“敌友”，这种分标签盖帽子的方法和共产党做的有什么不同？你不是真正的反独裁，你不是真的爱民主，你只是单纯的讨厌共产党，你只是一个讨厌共产党但是骨子里却被狼血腐坏了的独裁控，你就是XXXXXXXXX（省略一百万字）”。最后给人的感觉就是，这个“不敬神的YODA”是个比共产党还共产党的家伙，该杀。相对而言还是这个开坦克压人的共产党比较好一些，我们还是好好地闭上嘴巴拿脸蛋去蹭共产党的屁股沟卑贱地活着吧。<br />
<br />
如果你觉得上面这段话有点儿不好理解，那么我就归纳一下，我称这种方法为“一样论”：<br />
<br />
条件一～A强奸了B。<br />
条件二～B的老公很生气地踹了A一脚。<br />
结论～强奸是暴力，踹一脚也是暴力，所以B的老公和A一样邪恶。<br />
<br />
条件一～A强奸了B。<br />
条件二～B的老公和B做爱。<br />
结论～因为A和B的老公都操了B，所以他们都一样邪恶。<br />
<br />
其实说这么多，还不如古人一句话，我就此借用一下子：只许州官放火，不许百姓点灯。（请大家用天真烂漫的语气来跟我念：“放火和点灯嘛，都是一样样的嘛～”）<br />
<br />
<br />
一样论不仅仅可以用来向上面那样借力打力，还有别的用法，例如：<br />
装成一个谁都看不上眼的愤怒青年。<br />
五毛们摆出一副“哼，你们这些人只知道民主的好处却不知道民主的孬处，老子告诉你们政治都是邪恶的，无论独裁还是民主都一样。”这种说法表面上也有说到独裁是不好的东西，表面上是“很后现代主义政治正确的”，似乎隐藏在其中的逻辑是“我把一切跟政治相关的东西都贬低到地底，所以就能通过解构传统的正义与邪恶的观念来达至某种平等从而从根源上避免邪恶的政治，”，但是其实他要说的只不过是“政治都是邪恶的，所以邪恶是常态，常态就是正常的必须的，所以独裁就是正常的必须的。”说到底他装了半天的后现代愤怒青年只不过是拐弯抹角地通过灵活利用“一样论”，通过各打八十大板的方式来为独裁辩护罢了。五毛们只会在骂独裁的地方才会这样做――因为他们知道，在这些地方，先打独裁八十大板再打民主八十大板等于光打民主八十大板（独裁那八十大板早就给人打烂了，他打没影响，不打反而容易招人怀疑招人围观，打了多多少少都有了一件精灵斗篷可以装装石头）。<br />
<br />
<br />
<br />
说老实话，我实在很难想象一个人，如果不是五毛，怎么会既头脑混乱地为独裁辩护，又能够头脑清晰地灵活应用“一样论”来给我们放烟雾弹呢？不过想想，其实这也不是什么奇事，如果一个人从小被洗脑教育，后来在面对哗啦啦涌现而来的各种“不和谐”面前慌乱失措，这时候恰好有个五毛上来宣扬“一样论”，那么，他自然会很快得接受并学会这东西了。这种人，没有五毛钱拿却要站在别人那边，我称之为脑残。<br />
