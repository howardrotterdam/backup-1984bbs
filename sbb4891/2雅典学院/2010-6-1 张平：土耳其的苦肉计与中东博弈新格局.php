													<h2>张平：土耳其的苦肉计与中东博弈新格局</h2>
						
						
																											<div id="postmessage_651809" class="t_msgfont">今天，打着人道主义救援旗号，暗藏武器凶器的土耳其船队与以色列军队爆发激烈冲突，导致多人死伤的结局。帮助巴勒斯坦恐怖主义的“国际人道恐怖主义”这几年早已屡见不鲜，直接向以色列军人亮出屠刀这还是第一次。不过这些人无论怎么凶悍，毕竟只是被牺牲掉的过河卒子而已，如果我们只看到船上发生的那点事情，就错过了这场大戏的真正精彩情节――中东博弈新格局的诞生。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 新格局“新”在哪里？“新”在土耳其作为中东博弈的一方，在长期的酝酿之后，终于浮出水面，标志着奥斯曼新帝国正式加入逐鹿中东的游戏。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 明眼人一看便知，整场事件都是土耳其策划、组织、实施的“苦肉计”。土耳其不仅提供了船只、物资、通道，而且在事件发生后的第一时间便做出显然早就安排好的反应步骤――谴责声明、召回大使、内阁会议、一系列后续措施，等等。毫无疑问，流血事件是土耳其从一开始就安排好的。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 土耳其，而不是伊朗或者阿拉伯国家，这样旗帜鲜明地向以色列叫阵，在二战后的中东历史上，这是第一次。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 二十世纪的中东，特别是二战以后，基本上是阿拉伯与以色列两家扮演主角的舞台，土耳其与伊朗只是扮演配角。尤其是土耳其，一方面有来自苏联的巨大压力，另一方面还卷入了与希腊之间的塞浦路斯争端，自身则面临着与库尔德人之间的内战，因此只能依靠西方阵营的支持而生存，也就顺理成章成为以色列的盟友。进入二十一世纪以来，苏联解体，塞浦路斯问题趋向解决，库尔德武装日渐衰落，土耳其外患消弭，野心逐步膨胀，适逢国内伊斯兰主义崛起，“重温奥斯曼帝国旧梦”便不再只是说说，似乎付诸实施也指日可待。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 此时恰逢两次海湾战争之后阿拉伯世界全面衰落，以色列国小民寡，担负不起中东霸主的重担，所以土耳其便跟伊朗一样，开始跃跃欲试，问鼎中东，试图实现自己的霸主梦想。因此，二十一世纪的中东，不再是阿以两方争夺的天下，而是以色列、阿拉伯诸国、土耳其、伊朗四方混战的世界。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 在博弈的四方中，土耳其和伊朗其实是弱势的两方，论地利（土地面积和自然资源）它们比不上阿拉伯诸国，论人和（经济和技术水平）他们比不上以色列。因此两方中无论哪一方要崛起，都必须保证两个外在条件的存在：1、阿拉伯世界跟以色列国之间的持续冲突。2、阿拉伯世界的内部持续冲突。如果没有第一个条件，阿以之间的结盟将使伊土两国绝对无力染指中东霸主地位。而如果没有第二个条件，阿拉伯世界将成为中东无人可比的霸主，阿以之间实现和平的可能性也会大大增加。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 因此，土耳其和伊朗采取了极为相似的策略，也就是支持叙利亚――真主党――哈马斯这条强硬反以轴心，这不仅仅是因为这条反以轴心的存在会将中东和平化为泡影，而且是因为这条轴心同时还是阿拉伯世界的叛逆，通过支持它们，伊土两国左手打击了以色列，右手瓦解了阿拉伯诸国，同时还可以在伊斯兰世界里夺走“反对以色列侵略”的大旗，可以说是一箭三雕，妙不可言。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 伊朗早在20世纪80年代就已经看准这条轴心，开始动作；土耳其则从伊斯兰政党当选之日起就跃跃欲试，并从去年加沙战争后逐步登上舞台。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 目前的加沙船事件便是土耳其这种战略的实施步骤之一。事件的背景是加沙战争之后，哈马斯力量严重受损，并开始出现“法塔赫化”的倾向。一年多来，加沙地区基本上风平浪静，以巴之间的谈判紧锣密鼓。然而，阿以之间没人流血，这在土耳其看来是无法忍受的，无论是哈马斯放弃恐怖主义，还是以巴和谈出现进展，都是土耳其和伊朗的巨大战略资源损失。伊朗目前因为核武器问题自身难保，所以土耳其若不赤膊上阵，事情就有可能发展到不可逆转的地步。所以冲突是必须的，流血也是必需的，而时机是更加重要的，奥巴马刚刚打算跟以色列总理好好谈谈，以巴和谈开始有了眉目，没有比这个破坏和平希望的时机更恰当的了。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 随着土耳其的正式卷入中东博弈，抛开外部因素不论，未来的中东格局将取决于下列几个关键因素：<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 1、阿以之间的关系。只有以色列与阿拉伯温和派国家之间的事实同盟关系才有可能遏制伊朗和土耳其两国的野心。在去年的加沙战争中，我们可以清晰地看到以色列 ――埃及――巴解当局――约旦战线与土耳其――伊朗――哈马斯――真主党――叙利亚战线相对阵的情况，最近伊朗与土耳其巴西之间的核燃料协议其实也是这种关系的体现。就长远来看，阿以双方都必须调整战略，建立一种更为长期有效的同盟关系，防止被各个击破，这是双方利益的要点。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 2、取决于阿以双方手中的王牌。如果稳定的阿以同盟关系建立不起来（这是最大的可能），阿以双方将不得不各自为战，对抗土耳其和伊朗的攻势。这样，一切都取决于双方手中的王牌。就目前看来，在应对土耳其方面，双方手中最大的牌都是库尔德人。在加沙运输船事件的当天，库尔德独立运动武装袭击了土耳其的海军基地，这似乎可以被看作是对历史未来发展的一个预兆。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 3、伊土之间的同盟关系。在打击阿拉伯和以色列的势力方面，伊朗和土耳其有着明显的共同利益，在初始阶段，双方的合作（比如都支持叙利亚和哈马斯）是可以预期的，也会产生一定的效果，但随着事情的发展，双方争夺中东霸主的野心将迟早出现碰撞，如何调整双方间的关系，将成为未来中东发展的重要坐标之一。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 当然，这些只是就中东论中东，当外部势力――美国、欧洲、俄国、中国的势力被考虑进来之后，变数将会更多。就底线来说，土耳其的“下海”并非土国民众的福音，中东地区历来是个大泥潭，六十年来几乎所有卷入该地区冲突的国家，包括美苏这样的大国，都无不碰得灰头土脸。土耳其多年置身事外，本来养得白白胖胖，如今吃饱了撑的跑来押宝下注，早晚会有输掉裤子的那一天。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 总之，二十一世纪的中东不再是二十世纪的中东，用“阿以冲突”来概论中东局势的时代已经一去不复返了。</div>

							
							
							
							
