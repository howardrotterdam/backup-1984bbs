													<h2>那个叫鲁迅的终于从教科书里消失了</h2>
						
						
																											<div id="postmessage_510600" class="t_msgfont"><a href="http://www.tianya.cn/publicforum/content/free/1/1827052.shtml" target="_blank">http://www.tianya.cn/publicforum/content/free/1/1827052.shtml</a><br />
