<br />
外国公司可以一走了之，可是走不了的人，该怎么办呢？<br />
<br />
========================<br />
<br />
北邮一男博士生坠楼身亡 死者生前为人热情乐观<br />
<br />
2010年03月23日 来源：京华时报<br />
<br />
作者：孙乾 李靖 黄海蕾<br />
<br />
昨天下午，北京邮电大学内，一名2009级男博士研究生高坠身亡。目前，其坠楼具体原因仍在调查当中。<br />
<br />
事发北京邮电大学本部教学楼主楼，该楼有15层，属于“圈楼”，中间为露天天井。<br />
<br />
该楼一名保洁人员称，昨天下午1点50分左右，她正在楼道内打扫卫生，只听窗外传来一声闷响，接着又听到一阵惊呼。她前去查看，只见位于主楼内部北侧的一个露天天井地上躺着一名男子。天井四周窗户边有很多学生在张望， “他们都是上自习的，发现有人坠楼了，就都过去看”。几分钟后，学校老师和保卫处的工作人员来到现场维持秩序。紧接着，民警赶到勘察情况。随后，该坠楼男子的尸体被蒙上白布抬走，围观的人才渐渐散去。“没有人说得清他从哪层坠下来的，有的说10层，有的说11层。”<br />
<br />
昨天傍晚，主楼不时有前来上自习的学生，一名电子学院的本科生说，学校已经通知了，死者与其同在一个学院，是一名博士生。<br />
<br />
事后，北京邮电大学校务办公室、学生事务管理处在北邮人论坛上，对该起坠楼事件作出说明，证实下午1点50分，该校2009级博士研究生吴某某，在校本部主楼坠楼身亡。校方在说明中称，事发后，学校迅速启动了突发事件应急处置预案，校领导、保卫处、学生处和学院领导第一时间赶赴现场处理。目前，该生坠楼原因还在调查中。学校正在积极协调处理，情况有进展将及时通报。<br />
<br />
另据知情人介绍，根据初步调查，排除该生为他杀。<br />
<br />
校方帖子发出后，引起很多学生关注，截至昨晚9点，跟帖者已有1000多人。多数人都对该生的离去表示惋惜。<br />
<br />
平时为人热情乐观 近期曾有失眠状况<br />
<br />
昨晚，记者来到坠亡博士生小吴所在的宿舍楼。楼管工作人员称，吴同学所在的宿舍已经封了，校方正在等待其家长到来。该宿舍原本有四个床位，其中一个人开学就没有来报到，还有一名博士是在职教师，早已搬到一楼去住，因此他的宿舍只有两人。现在该生出了事，另一人也搬出。“他是个非常热情的孩子，见了面总打招呼。”楼管员称，今年寒假结束后，吴同学返校后发现宿舍灯泡坏了便找人来修，“修好后，一口一个谢谢，是非常客气的人”。<br />
<br />
知情人士介绍，吴同学26岁，没有女友。根据该生所在班的班长及舍友的描述，该生平时很热情也很乐观，跟同学关系也不错，但近一阶段他可能出了点问题，有失眠的情况，具体什么事不明。据了解，吴同学家庭并不完整，他的日记本上模模糊糊地记载着一些家庭生活片段，还有一些慨叹，没有具体细节。<br />
<br />
============================<br />
<br />
然后，我在网上读到了他的遗书。<br />
<br />
============================<br />
<br />
遗书<br />
<br />
想好了。我想我真的想好了。<br />
<br />
这个世界是一沟绝望的死水，我在这里再怎么折腾也激不起半点涟漪。所有的努力都会被既得利益集团踩在脚下，所有的奋斗都面临着举步维艰。<br />
<br />
冷漠的人，谢谢你们曾经看轻我，让我最终下了这样的决心。世俗的炎凉，尘世的丑恶，恶心的嘴脸，可恶的压力，你们都随风去吧。<br />
<br />
对不起。对不起。<br />
<br />
我可怜的让我万般不舍的妈妈，最对不起的就是你……<br />
<br />
我此刻一想到你苍苍的白发，粗糙的双手，一年大部分的饭菜只是馒头就咸菜，一件蓝色的夹克穿了那么多年，每年过年都是那一件，我都会心痛得要死。<br />
<br />
儿子不孝，不肖，没能让您的付出得到回报，这么多年您一个人含辛茹苦供我一直读到北邮的博士啊……曾经，我所有对您的承诺，都没有办法履行了，对不起，对不起。儿子是一个没有用的人，自从离开老家以来，其实无时无刻不在想着让您也能来北京吃一次烤鸭，看看天安门和故宫，住上楼房，坐上小车，不用再为了我去捡垃圾、拾破烂，不用再去给人家工地上做饭赚钱瞧人家白眼……可是，这么多年了，我什么都没有给您，我甚至连您一直想要却舍不得买的假牙伴侣都没能给您买到，每当晚上躺在床上想到您每顿饭都在用那戴不牢的假牙痛苦地磨着干涩的馒头，我的眼泪就止不住地流……一百块啊，不过是一百块啊，一百块还不够那些老板官员们抽一盒烟的钱，我却给你买不起！<br />
<br />
我太没用了。现在知识太没用了。有用的只是金钱和权势，有用的只是关系和背景。现在要凭正直的才华去出人头地，太难太难了。我也曾试着找过工作，但是没有人用我。我对这个世界彻底地绝望了。绝望了。我的肝又开始痛了，不能再写下去了……<br />
<br />
我走之后哥哥一家就应该会对您好点了，保重……<br />
<br />
终于可以一了百了了，我很高兴。永别了，妈！<br />
<br />
2010年3月22日<br />
<br />
======================<br />
<br />
下面是我的感想。<br />
<br />
我特别理解他。只有在国内大学读过博士的人，才能体会那种知识带来的绝望感。你付出了那么多，却一无所获，甚至连知识也学不到，因为大学不过是社会的缩影，所有社会丑恶现象在校园中全部存在，而且因为披着学术的伪装，而显得更加虚伪。当你看到那些所谓的学者，不过是道貌岸然的学棍，而整个学术的体系和制度，都在鼓励急功近利和弄虚作假时，你的信念瞬间就崩溃了。这样的苦苦忍受为了什么呢？学到知识又有什么用呢？你只有变得和其他人一样虚伪，才能熬出头，这样的未来值得追求吗？<br />
<br />
曾经有一篇博士毕业论文，它的致谢是这样写的：<br />
<br />
　　“从进入大学到博士毕业，整整十年了。在这十年中，我过得浑浑愕额。<br />
<br />
　　十年前，我身高170cm，十年后，我身高还是170cm。十年前，我体重60kg，十年后，我体重还是60kg。十年前，我一无所有，十年后，我还是一无所有。十年前，我眼睛明亮、有神，十年后，摘掉眼镜，我已看不清自己有多少个手指了。十年前，我声音洪亮、清澈，十年后，已经是慢性咽喉炎，声音嘶哑。十年前，我踌躇满志、指点江山、激扬文字，十年后，我心如止水，只求温饱。<br />
<br />
　　当然，我也得到了一些东西。<br />
<br />
