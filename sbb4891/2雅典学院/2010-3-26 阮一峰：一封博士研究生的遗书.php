													<h2>是不是有点似曾相识？</h2>
						
						
																											<div id="postmessage_533406" class="t_msgfont">==============================<br />
<br />
&nbsp; &nbsp; 中国人的人格缺陷之一可以称为“考试综合症”。十几年的严酷考试，必然会塑造一种与众不同的民族心理。我相信当代中国人，每个人都存在这种心理疾病。由于考试成为生活目标，赤裸裸的功利主义上升为精神生活的本质。在中国，不是对真理的追求，也不是对爱的追求，而是对活着或更好地活着的追求成为全部生活目标。对于中国学生来说，考试――做人上人，是天经地义的真理，是天经地义的善。由于威权统治，由于竞争残酷，为实现这种理想，孩子从小就学会了机会主义的生活态度：极其发达的名利欲，生活的极端不安全感、对竞争的病态紧张，以及不择手段地通过残害别人而出人头地，对受害者落井下石或者毫无同情心并对所有人的不幸倾向于幸灾乐祸――残酷竞争意味着人对人是狼。<br />
&nbsp; &nbsp; 这是一种令人绝望的现实。政治入教则进一步鼓励了学生的机会主义态度。比如，三好学生和入党，由于教师控制名额，讨好教师和排挤同学就成了自然的进取策略。我们悲哀地发现，即使在年龄很低的学生当中，那种“成熟”，不讲真话、揣摩对方心思、迎合提问者等等，极为普遍。我们的社会已经没有了孩子，只有一些小大人，而且是有严重的人格缺陷的小大人。易言之，学校教育培养的不是正直、智慧的毕业生，而是年轻的名利之徒。<br />
<br />
&nbsp; &nbsp; 正是这些年轻的名利之徒构成了我们当今社会的基础。他们唯一感兴趣的是名利，而不是是非――即使他们大意凛然地谈论是非或道德高调的时候，你也能发现他们仍然是为了利害。他们可以一边谈宽容和爱一边杀害任何一个可以表现自己伟光正的陌生人。他们甚至比老一代人更无道德感，更精明于投机取巧和趋利避害。他们象一群急于挤上分赃的海盗船的乞丐，疯狂挤进体制内，打算在风暴来临之前抢更多的东西。这种犬儒主义状况是非常严重的，这也和90年代全社会的犬儒互为表里。<br />
&nbsp; &nbsp; 我曾经和一些在省府工作的新来的毕业生交流，令我吃惊的是他们对领导的曲意逢迎，更令我吃惊的是这种经验之娴熟，那种“自然”。在一些重大的社会事件上，他们毫无同情心，也毫无是非观念。不仅如此，是非是成熟嘲笑的对象。他们是从哪学来的呢？他们是中国学校“合格”的毕业生。<br />
&nbsp; &nbsp; 这种生存态度从根本上说，来自生存恐惧和对未来的不安全感。青年人愿意付出道德上的代价以便尽可能多地在社会上找到一个落脚点，以便在“吃不饱穿不暖”之“前吃多占”，或者基于同类原因，通过出人头地来成为他人仰视的“权威”。无论是前者“急匆匆的立业”，还是后者“恶狠狠地成名”，都和他们对生活的艰苦记忆以及关于这种艰难的教育密切相关。<br />
&nbsp; &nbsp; 由于我们已经讲过的历史性原因，中国人生活的确非常艰难。这种记忆是真实的，它是“实用理性”这种人生态度产生的主要根源。但问题是，另一方面，对生存的恐惧完全是真实或现实的吗？我觉得在一种程度上，这种恐惧本身制造了新的恐惧，并把恐惧放大，使人成为恐惧的奴隶。<br />
&nbsp; &nbsp; 我们的家庭和学校，反复告诉孩子们生存维艰，资源有限，因此不拼不能活，不搏不能“赢”，不挤不得食，不坏不富贵。于是人成了一个紧张的生活机器，没有精神生活，没有社会生活，只有活着，拼命地跑，片刻不喘息。现在的问题是，这种生存恐惧本身强化了生存紧张，而事实上情况也许不这么坏。或者说，本来情况可以好转，恰恰是由于这种你死我活的“拼搏”，社会忙于分配而忽视了生产，或者忙于生产而忽视了创造发明，我们的生才越来艰难。<br />
<br />
&nbsp; &nbsp; 不仅如此，我觉得渲染生活的艰难在一定意义上也出于家长、教师和社会的某种习惯性恶意。为了各自的利益（如社会的稳定利益、家长培养孩子感恩、教师显示自己重要，等等），成人煽动了孩子对生活的恐惧感。有人说，“除了人以外，最强的结合里仍然是政治家和政府首脑使用的恐惧这个手段。政治家煽动人民对敌人的恐惧，也煽动人民对紊乱的恐惧，声称有秩序的社会具有导向作用，会给我们带来导向和安全。……用恐惧来控制人毕竟是一种统治策略。”（《哲人小语――我与他》卡林-瓦尔特 <br />
编 三联书店1994年出版，P518。）出于类似的理由，教育家则煽动学生对生活的恐惧，声称只有接受教育并按他们的意见接受教育才可能获得生存。急于奔命使孩子们失去了社会关怀和对真理的爱，成了麻木不仁的臣民，而缺乏公民兴趣。但正是这些勤劳的蚂蚁构成了我们校园体制的合法性，构成了我们社会的基础。<br />
&nbsp; &nbsp; 生活是艰苦的，但这种艰苦的责任在成人世界，而现在压力却全部转移给了孩子。资源是短缺的，但有些夸大其词，这种夸大其词培育出生存着的动物，却没有培育出健康的人。<br />
======================================<br />
