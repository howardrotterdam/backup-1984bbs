			<div class="postmessage">
								<div id="postmessage_536497"><a href="https://1984bbs.com/redirect.php?goto=findpost&amp;pid=536467&amp;ptid=40771" target="_blank">https://1984bbs.com/redirect.php ... 6467&amp;ptid=40771</a><br />
<br />
自由和尊严这些东西可比动产，领土好比不动产。坚持领土优先的人也有他们的道理。<br />
<br />
举个例子，相对领土这种不动产，自由和尊严这些东西去的容易来的也容易。一个民族这一代没自由和尊严，下一代也许就会多些，再下一代就会更多。这些东西是软件，社会在进步，人类在发展。而领土这东西，同样因为社会在发展，人类再进步，很可能过了这个村就再没这个店，一旦丢了，不是说要回来就能要回来的 -- 当初别人武力从你这儿夺走的，你今天未必就能再用武力夺回来 -- 这样的事儿太多了。你说这不公平，凭什么你当初抢得，我现今就抢不得？你别问我，你问二战的日本军阀去 -- 他们当初就是这么想的，结果原子弹平步青云 -- 因为时代在进步，大英帝国抢的时候，大英帝国是英雄；轮到你抢了，你就是贼！所以说，领土这种东西，时效性很强。就好比当初中国好多领土都是满清弄丢的（当然本来也是满清带来的），可中国共和了民主了民国了，大家自由和尊严扩大了，当年那些丢了的领土，照样要不回来是不是？<br />
<br />
换句话说，自由尊严政治制度民主自由这些东西，都是有改进和回旋的余地的。商纣再残暴，人走茶凉，还有周武上台的机会。但如果因为商纣残暴就把中国分了一半给别人，比如说给犬戎，那将来就算周武上台，拿到手里的也是半拉中国，成色减了不少。<br />
<br />
这么一来，在极端情况下，就会有两个针锋相对的意见。假如说在商纣残暴的时候，有人说，比如犬戎说，我帮你们灭了丫挺的，但是作为回报你们得割一半的地给我。普世价值的信奉者可能会说，割了就割了，如果没有自由和尊严，要领土也没用。而国族主义思想重的人可能会说，你这是病急乱投医，就像缺点钱花就卖祖宅一样。商纣再坏，也不过是河里的流水，国家的领土却是山上的石头，领土就算不割，商纣也会完蛋，自由和尊严会改善，只是时间早晚问题。可领土一旦割了，很可能就再也要不回来了，到时候拿到了自由和尊严那也是个次品。因此从长远看，他能刻忍受商纣一时的残暴，也不要做割让领土的败家子 -- 因为历史是没有终结的，假设最终自由和民主一定会来到，可能是早50年但是领土少一半，也可能是晚50年但领土保住了 -- 这两种情形，哪一种更好？<br />
<br />
在我看，排除判断上的分歧，这就是个价值观的问题 -- 因为对从第50年到第100年间的那一带人影响最大，是现在就要拿到自由和民主，但要以后代的领土打折扣为代价？还是为了后代能有个更大的舞台从而牺牲自己这一代人？这就好比日本打进来了，是为了先把领土保住从而不得不暂时忍受蒋光头的独裁专制把日本人赶走再说？还是抓住这个“历史机遇”以牺牲主权和领土完整为代价借刀杀人削弱蒋光头的统治基础从而加速“民主化”的到来？<br />
<br />
这两种针锋相对的价值观，你支持哪一个？<br />
