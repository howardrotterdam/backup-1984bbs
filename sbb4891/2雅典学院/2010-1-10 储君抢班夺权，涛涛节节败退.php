2010年1月5日，习近平出席全国基层党建工作手机信息系统开通仪式，并通过全国基层党建工作手机信息系统，向全国１００万名基层党组织书记、大学生“村官”发出问候短信：<br />
&nbsp; &nbsp;&nbsp;&nbsp;“全国基层党建工作手机信息系统今天正式开通！我代表党中央，向全国的基层党组织书记、大学生村官致以亲切的问候！习近平”<br />
&nbsp; &nbsp;&nbsp;&nbsp;(博讯 boxun.com)<br />
<br />
&nbsp; &nbsp; 此事非同小可，要害是我代表党中央。这是不折不扣的盗用党中央名义。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 如果说1969年林彪根据毛主席的提议发布有关战备疏散安排的六条指示，总参作战部以“林副主席指示第一个号令”的名义向下传达到各军区，是总参缺乏政治头脑，只知道拍林彪马屁，第一个号令用词不当，为写进党章的接班人副统帅林彪惹来杀身之祸，那么现在习近平擅自盗用党中央名义公然我代表党中央，则是有强大后台的、预谋的抢班夺权一步棋，绝对不会为习近平惹来杀身之祸，相反成为习近平推波助澜接班总书记的成功一击。现在的军委主席党总书记胡锦涛敢怒而不敢言，只能保持沉默。我们不能用毛泽东处置林彪的老套来评估现在的形势。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 因为党和国家“第一号”的名器，毛泽东是实至名归，有“名”有“器”；然而胡锦涛是只有虚“名”而无实“器”！ 奈之何也！<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 理由很简单，现在的政局，与毛泽东、林彪时代1969年截然不同，那时的军委主席国家主席毛泽东一言九鼎，政治局常委100% 对毛泽东俯首帖耳绝对服从；而现在的军委主席国家主席党总书记胡锦涛，不能一言九鼎，只能一言一鼎――政治局九名常委，胡锦涛只能投一票，大多数常委是江曾安排进入政治局常委的，难免听命于早已下台的江曾，曾当年退出政治局常委就是“一下三上” 为筹码。在政治局常委胡锦涛是少数，处境几乎类似于光绪皇帝。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 设想一下，在江任总书记军委主席期间，早就是政治局常委的军委副主席胡锦涛敢擅自公开说“我代表党中央”吗？当然不敢，否则多半是“篡党夺权”罪责难逃；即使江授权他他也没胆说。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 再看看15届中央全会才升为候补中央委员、17大坐火箭升任政治局常委的习近平，现在连军委副主席都不是，居然敢于冒天下之大不韪公开说“我代表党中央”，真是“与时俱进”啊，对比这个巨大差距，实在有违常理，岂不蹊跷？连军委副主席都不是的人，擅自盗用党中央名义，这样的大事，背后没有后台撑腰（甚至背后指使），可能吗？<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 对于习近平擅自盗用党中央名义一事，中央办公厅、胡锦涛办公室保持沉默，说明了什么？说明他们投鼠忌器，敢怒而不敢言，无可奈何。政治局常委也不会为此作决议让习近平检查。而习近平则是有恃无恐，更无须置辩。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 习近平擅自盗用党中央名义这一招，影响深远，棋胜一着。“全国的基层党组织书记、大学生村官”虽然没有投票选举中央委员、政治局委员和常委的权利，但是使“全国的基层党组织书记、大学生村官”惊喜，收买了民心；又作为新闻向全国发布，等于全国的基层党组织和全国人民制造了习近平18大接班党总书记军委主席已成定局的舆论。中央办公厅、胡锦涛办公室对此保持沉默，很容易被理解为“默认”。<br />
