													<h2>鲜为人知的姬鹏飞自杀真相</h2>
						
						
																											<div id="postmessage_711033" class="t_msgfont">姬鹏飞的儿子姬胜德，是中国人民解放军原少将、总参情报部常务副部长，军事法庭认定姬胜德犯有的三项罪行足以量判死刑，姬鹏飞是在自认经多方为子奔走请命无效，认为姬胜德必死无疑的情况下突然死亡。由于姬鹏飞历任国务院副总理、国务委员、港澳办主任、人大副委员长、中顾委常委等职，中央授权新华社发了一则消息：久经考验的、忠诚的共产主义战士，无产阶级革命家，我国外交战线的杰出领导人，第五届全国人民代表大会常务委员会副委员长，国务院原副总理，原中央顾问委员会常委姬鹏飞同志，因病医治无效，于2000年2月10日13时52分在北京逝世，享年91岁。根据姬鹏飞生前担任的职务，他的追悼会的规格应有江泽民、李鹏、朱熔基出席，但却只有胡锦涛出席，致了简短的悼词。更为蹊跷的是，中央军委、军方的四总部、国防部都没有送花圈。 最令人不解的是，姬鹏飞的儿子姬胜德，当时已被军事检察院宣布逮捕，却意外地穿著便装，出席了乃父的追悼会。胡锦涛按惯例，向死者家属握手节哀时，也握了姬胜德的手。这一镜头在电视荧屏画面上出现后，曾引起很大争论：这算怎么一回事？ 综合以上种种迹象，说明姬鹏飞的死颇不寻常。 <br />
<br />
■ 姬鹏飞原来是自杀身亡的 <br />
<br />
姬鹏飞的追悼会之后，党内党外引起了很大反响，2001年10月下旬，中共中央、国务院、中央军委、中纪委发至省、部、军级党委(党组)文件：关于姬鹏飞的政 治、组织结论补充意见：“姬鹏飞就其儿子姬胜德的问题，曾向组织提出了不合法、不合理的要求，被拒绝后，做了、讲了一些严重错误的事和话：以极其错误的行为，造成原患病症恶化而死亡；姬鹏飞一生曾对党的事业，对国家外交，港澳等工作，作出较大贡献。经讨论并听取了多方面的意见，决定维持对姬鹏飞悼词的结论；但建议：今后对有关姬鹏飞生前活动等，不举办公开形式的研究和纪念。”全文不到二百字的补充意见称：姬鹏飞是“以极其错误的行为，造成原患病症恶化而死亡，”这无疑含蓄地暗示姬鹏飞是自杀身亡。当然，这也是继高岗之后，中共体制内第二起高层领导人的自杀事件。<br />
<br />
■ 姬胜德落网经过 <br />
<br />
姬鹏飞之子姬胜德，原任总参情报部常务副部长，（部长一直由副总参谋长熊光楷兼任）。一九八年初，姬胜德曾临时主持过情报部工作，后在审核中发现其生活腐化糜烂，常和不正经的人往来等，所以军衔一直是少将，职务也一直是副部长。<br />
<br />
姬胜德是美国、台湾策反对象，而姬胜德本人似乎也有出走或至少是留後路的迹象，秘密将妻儿安排到美国定居，其妻在美国有巨款存款和两座价值百万美元的豪宅，此事在美籍华商锺育翰涉嫌非法政治捐款一案牵涉姬胜德，被美国媒体大肆报导後，北京高层才知道。<br />
<br />
一九九年三月中旬，姬胜德在珠海接获通知，让他赶回北京玉泉山参加军委扩大会议。姬胜德赶到会场一看，发现气氛不对劲，无人跟他打招呼。只有迟浩田对他说：近年来你的业务很忙，该休息休息了。姬胜德一听，立即发了呆。迟浩田接著宣布：经中共中央、中央军委核准，中纪委、军纪委决定：自即日起留点审查(即隔离审查)。迟浩田问姬胜德：你有什话要讲？姬说：我父亲知道没有？我想见见张万年、尉健行。又说：我会老实交待我的生活问题的。在他身旁的中纪委副书记、军纪委书记周子玉立即对姬说：如果是生活问题，会对你采取留点审查措施吗？有什问题，你很明白，组织也很清楚。<br />
<br />
■ 留点审查期间的姬胜德 <br />
<br />
姬胜德在留点审查期间，仅交侍了曾奸污女青年、收受过赖昌星、中资港商的金钱等问题。他在此期间，先后搞过多次“绝食抗议”和一系列的企图自杀。他曾用袜子做成绳子勒紧自己的喉咙自杀，用牙刷塞喉自杀，甚至在提审时突然冲向窗口要跳楼自杀。到了一九九年六月，由军纪委书记周子玉、副总参谋长熊光楷代表军事检察院对姬胜德宣布：依法逮捕。姬胜德听了宣布，当即瘫倒在地。<br />
<br />
■ 姬胜德的三项罪行<br />
<br />
一九九年八月初，军事法庭开始对姬胜德案进行审理，认定姬胜德犯有三项罪行：(一)收受犯罪集团人民币、美金、港币贿赂，折合共二千一百三十多万元人民币，其中有一千五百九十万元被他套汇成外币，在外国开设了私人账户；(二)挪用、侵吞军事用途的资金九百七十五万，已挥霍和给家属在海外置业达九百万元人民币；(三)长期隐瞒、欺骗组织其配偶加入外国国籍的事实，隐瞒本人和社会上、香港、外国组织的不正当关系，并透露、泄露了军方机密等。这三项罪行中的任何一项都足以量判死刑。中央军委审议意见是死刑，中纪委审议意见也是死刑。中央政治局审议时，支持军事法庭审议的意见：死刑。<br />
<br />
■ 姬鹏飞为子请命 <br />
<br />
当时正在北京香山养老的姬鹏飞得知其独子罪行足以判死刑后，先后四次写信给江泽民、张万年、迟浩田，请求宽恕姬胜德，免其一死。姬鹏飞还向薄一波、宋任穷、万里、宋平、谷牧、张爱萍等老同志请求协助，向中央政治局陈情。但，这些老同志都很为难，主要因素是姬胜德长期隐瞒、欺骗组织，本人身居要职却与外国有不正常关系并泄露了军方机密，这些罪行过于严重，致使他们不愿、也下敢为其说情。<br />
<br />
■ 姬鹏飞破口大骂 <br />
<br />
二OOO年一月中旬，中纪委、中央办公厅派出中办主任王刚，到北京香山姬鹏飞养老处，作了简单答覆：中央和江泽民同志看了来信，作了郑重讨论，认为姬胜德案情十分严重，在党内、军内已引起公愤，对于量刑，将按法律程序进行，并请姬老平心些，安度晚年。王刚在谈话中透露：死刑是难免的，但能推迟一、二年执行。暗示内定“判处死刑，缓期二年执行”，实际上就是暗示免于姬胜德一死了。不幸的是，姬鹏飞没有听懂王刚说的“死刑是难免的，但能推迟一、二年执行”这句话的真正含义，只认为其子必死无疑了。姬鹏飞听了之后，破口大骂：凭我和老伴为党为国奋斗近七十年，共产党就不能刀下留情，给我独子留条命。要死，我就死在中南海！又骂道：见个政治局常委，要比当年见毛泽东还难，这是什么世道！ <br />
<br />
■ 为儿子免一死做最后努力<br />
<br />
二OOO年春节前夕，中共中央对老同志进行登门拜访，被姬鹏飞拒绝了，并说：我要见儿子！他又向中央提出要求，能否让姬胜德春节时回家一聚，再返监狱服刑？得到的回答是：根据现行司法规则，疑犯不能假释返家过节。于是，姬鹏飞相约了十多名平日来往较近的老同志，节日到他家聚餐，趁此商议一下如何为免儿子一死作最后的努力。但，结果这十多名老同志都托词节日忙，婉拒了到姬府聚餐。<br />
<br />
■ 姬鹏飞用红酒吞服安眠药 <br />
<br />
二月八日中午，姬鹏飞在书房写了遗嘱之后，用红酒吞服了三十多粒安眠药片入睡了。工作人员发现后立即将姬送三军医院抢救。姬鹏飞被送院后已处于临床死亡，但院方仍用生命仪器和药物来维持微弱的心脏跳动，为的是等候中央指示如何宣布他的死亡，而采取的延续生命措施。到二月十日中午，生命仪器也无能为力了。<br />
<br />
 ■ 姬胜德是如何参加其父追悼会的？<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; 在姬鹏飞追悼会上，胡锦涛向姬胜德握手致哀<br />
<br />
姬胜德之出席其父的追悼会，是其母许寒冰奔走求情求来的，是许寒冰哭到薄一波家里请求的。当局批准同意姬胜德出席追悼会，张万年提出了附带条件：必须遵守规则，追悼会结束后必须返回，不能送灵车王八宝山火化场。如果在会场搞事，要承担一切严重后果。对此，许寒冰、姬胜德都签字作了保证，姬胜德才得以出席其父的追悼会。姬胜德参加完父亲葬礼后，被关在总参监护所，他感觉前途更加无望，于8月13日用牙刷柄割脉，并吞服70多片安眠药自杀,经抢救幸免于死。许寒冰要求江泽民准予姬胜德以高血压症为由保外就医被拒,又提出每周探望三次、送食品不受限制的要求遭拒,许悲愤难抑,2001年9月14日晚吞服安眠药自杀,被301医院抢救过来。 <br />
<br />
2001年，中国人民解放军军事法庭认定姬胜德收受赖昌星巨额贿款，并有数千万港元资产说不清楚来源，检方指控受贿罪、贪污罪及挪用公款罪等多项罪名成立，一审判处死刑，缓期两年执行，姬德胜不服上诉。中共高层认为姬胜德曾在收集情报等工作方面对国家有过重大贡献，他在后期能主动交代问题，积极退还赃款，并主动揭发其他人的违法乱纪行为，有立功表现，当然，也有可能江泽民缓和了非杀不可态度，因为姬鹏飞和江泽民的继父江上青曾是患难与共的战友。2002年终审判决，将一审死刑缓期两年执行，改为无期徒刑。后来，由于姬胜德患有严重心脏病，法院同意其保外就医，被送到京郊一军队医院治疗。但同案其他人则没有那么幸运，通过对姬胜德通谍、走私、贪污等问题的调查，在总参就至少牵出十余人，2000年12月，引发总参情报部出身的国防部美洲大洋洲局长徐俊平大校于叛逃美国。<br />
