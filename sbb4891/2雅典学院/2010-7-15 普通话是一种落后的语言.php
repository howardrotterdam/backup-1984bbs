													<h2>普通话是一种落后的语言</h2>
						
						
																											<div id="postmessage_741254" class="t_msgfont">2010-07-15 00:48:20　来源: 南方网　跟贴 335 条 手机看新闻 <br />
以方言为代表的古汉语成了大力遏制的糟粕，而被奴化的普通话成了官方语言。更让人失望的是，胡化后的汉语由于过于简陋，歧义过多，以至于现在专业性强一点的演讲，在不允许用粤语时，必须要用英语说，或者每个人必须要有份演讲者的稿子。<br />
----<br />
我是一个北方人，说着标准的普通话，没有经过任何训练，轻松的过二级。但，随着英语水平的不断增强，发现汉语有很多地方不如英语，至少在准确性上，差了很多，记得以前有很多人都说汉语比英语先进，可是问他们原因的时候，最多只能说出个博大精深，因为一词多义，而，一词多义，却正是一门语言落后原始的标志，证明了这门语言的词汇少。可是，在上大学后，以及后工作接触到很多南方人，发现他们依然肯定汉语，认为即使不是最先进最准确最优美的语言，也是所有语言的佼佼者，但不同的是，他们能从音色，用词等很多方面说明，有理有据。可惜我不懂粤语和客家话，他们的观点，我没有记住，也无法重述，所以我从历史的观点来分析这件事。<br />
<br />
中国在宋以前，说的是客家话，或是类似闽南话，或是粤语的方言。后来五胡乱华，中原人被迫南迁，于是古代汉语被带到了江南，其中客家人，便是正宗的古代汉族后裔，他们操纵者原汁原味的汉语。现在的官方语言，是被称为北方方言的，这是被胡华的语言，在历史上，简陋的胡语混血了优美的古汉语，使得汉语变的简单，除了汉藏语系基本的主谓宾的语法结构外，汉语基本被打的支离破碎面目全非，最终被阿尔泰语系胡化，这种方言在语言其准确性，实用性，远远的退步了，都远不如南方方言先进。语言退步的同时，中国的人才结构，文明的范围也发生了变化。<br />
<br />
先来说文化方面，古汉语普及时期，产生了汉赋，唐诗，宋词（元曲，基本等同于宋词，而在造诣上，远落后宋词，所以这里就不提了）。在政治上，忘记古代汉语的北人无力抗争外族，导致南方沦陷，宋朝灭亡，这是中国人第一次亡国，被蒙古杀掉的人又6000多万（更可悲的是，中国人竟然将成吉思汗当做民族英雄崇拜）。之后是明朝，从这个朝代开始北方方言开始作为官方语言，也正是这个朝代的建立，标志了中华民族衰弱的开始。<br />
<br />
接着是清朝，离我们现在最近，也是我们最熟悉的，我们考察清末至今的中国历史，可以说就是由南方人写就的－－洋务运动，太平天国，维新变法，辛亥革命，国民革命运动，共产主义运动，改革开放。引领时代的人物，也基本都是南方人－－李鸿章，康有为，梁启超，孙中山，蒋介石，毛泽东，还有建国至今的中国党政领袖基本全是南方人。北方人我只能想到叶赫纳拉氏和袁大头几位了。科学，艺术，文化菁英，南方人也是占统治地位的。<br />
<br />
我们现在所谓的官方语言其实却是被奴化后的语言，如果说汉语伟大，请明确，那是指古代汉语，汉族人优秀，那是指具有正宗汉族血统的汉族人，这就能解释，为什么同样抗战沦陷区，南方抗战英雄多，自古状元绝大多数出自南方。<br />
<br />
我并不是一个种族主义者，我也不相信血统决定智商优劣的那一套，<br />
<br />
但南北地区的人的智商差异也的确是存在的，而且非常明显。<br />
<br />
原因我认为与两者所使用的语言不同有关。<br />
<br />
北方人与南方人的语言不同<br />
<br />
在英语中是没有Chinese（中国语）的说法，只有Mandarin和Cantonese的说法。<br />
<br />
Mandarin指的是普通话，也就是北方话的代表，其词源自于mandareno（满大人），意思就是指那些满洲官员所使用的语言。<br />
<br />
Cantonese指的是南方话的代表--广东话。<br />
<br />
由于北方自唐末以来就长期由北方的契丹、女真（满）、蒙古等民族统治（主要是阿尔泰语系），所以北方话也就难免被阿尔泰化，从而丢失了大部分汉语方言所具备的特征，还带进来某些外族口音。<br />
<br />
比如大部分汉语方言能够分尖团音，北方话就分不了；<br />
<br />
大部分汉语方言能够区分入声和非入声，北方话就分不了；<br />
<br />
北方话的声调只有四声：阴平、阳平、阴上、阴去。<br />
<br />
而古汉语的声调分为八声：阴平、阳平、阴上、阳上、阴去、阳去、阴入、阳入。<br />
<br />
南方话中除了粤语外大多也有声调丢失的现象，但所保存的声调都较北方话为多，均在六声以上。<br />
<br />
而粤语不单止完整保存了八声声调，而且还在阴入、阳入中间分出了一个中入，从而变成九声声调。<br />
<br />
北方话的所谓轻声，实际就是声调丢失，这种现象是母语无声调的满蒙族人留下的特征，其他方言区都没有轻声现象；<br />
<br />
北方话中的儿化音非常多，也是其他方言所没有的。<br />
<br />
阿尔泰语系的特点是语素少，音节多，最极端的例子是日本语。<br />
<br />
北方话相对于南方话也有这个特点，这就给表达带来很大困难。<br />
<br />
比如稍微学术一点的普通话演讲，光用口语是不行的，必须辅以文字说明，<br />
<br />
因为音素少而导致同音词现象太多了。所以，为了减少同音词，北方话不得不增加词汇的音节数量，北方话中的单音节词汇极其的少，而多音节的词汇却特别的多。<br />
<br />
而南方话即相反，由于其音素多，同音的现象少，所以单音节词汇非常丰富，三个音节以上的多音节词汇很少，并且多是自北方话里引进而来。<br />
<br />
这样一来，表达同样意思的一段话，南方话可以用比北方话少得多的音节，其效率自然而然就高得多了。<br />
<br />
北方话中由其令人不理解的就是其儿化音，平白无故增加一个音“r”，使本来就效率不高的语言变得越加低下。<br />
<br />
如果你对比香港电影的粤语版与国语版，你就会发现用普通话译制粤语电影时，常常有删减语言内容的现象。<br />
<br />
原因是广东话用十个音节就能说明的话，普通话非得用十五、六个音节才行，为了对口型只能删减内容。<br />
<br />
