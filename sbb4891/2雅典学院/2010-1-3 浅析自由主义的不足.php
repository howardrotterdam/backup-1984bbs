													<h2>浅析自由主义的不足</h2>
						
						
																											<div id="postmessage_411185" class="t_msgfont">浅析自由主义的不足<br />
<br />
一<br />
儒学内圣外王，内学为道德学、心性学，外学为政治学社会学。外王立足于内圣，内圣开发为外王，内外不二，圣王合一。孟子曰：“人有恒言，皆曰天下国家。天下之本在国，国之本在家，家之本在身。”东海加上二句：身之本在心，心之本在仁。仁，于宇宙为本体，于生命为本性，乃是人身乃至天下国家的根本。对此，自由主义缺乏必要的认知。<br />
<br />
自由主义解决的是社会、政治层面的问题，说透了，它主要是一种利益分配机制，范围上只相当于儒家外王学的一个分支。（这也是方便说，盖脱离内圣谈论和追求外王，就会偏离正道、沦为异端，如荀子，就是儒门中的外道异端。严格地说，脱离内圣的外王，已非真正王道。）<br />
<br />
自由主义对人性、对生命本质的认识甚为肤浅，只知习性之恶，不识本性之善，只知人身之小，不识本心之大，其思想体系的偏浅和道德资源的不足是显而易见的。正如东海《荀子论蔽、荀子之蔽及其它》一文所指出：“自由主义比较先进文明，真理度颇高，却也有蔽，蔽于习而不知本：了解、重视人的习性，但为习性所蔽，不识本性；或叫蔽于人而不知天（人，人欲之习恶；天，天性之至善），蔽于欲而不知德：尊重人的欲望但不识人的本性，不悟良知之奥。”<br />
<br />
同时，自由主义者往往以民主为至上，为唯一选择和追求，甚至成了拜民主教的信徒，这就把民主这一手段目的化乃至神圣化宗教化了，就象当年将“革命人民”将“革命”神圣化宗教化一样。以中华文化的眼光看，自由主义缺乏心性基础，属于无“本”之学、无“体”之学。民主自由作为一种信念、一种政治追求和理想当然毫无问题，但不足以成为生命信仰。<br />
<br />
自由主义固然有助于社会道德的提升，但这种“助”是间接和消极的，是通过制度建设和底线设置去完成的。它不仅对于个体的道德建设和心性圆满缺乏主动、积极、全面、深刻的追求，并且很容易从个人主义滑向利益主义以及利己主义。<br />
<br />
二<br />
自由主义致力于思想启蒙和民主追求，无疑是值得敬佩的，但是，由于上述原因，<br />
中国的自由主义者在开展思想启蒙和民主追求时，往往“启”之无道，求之乏力。<br />
<br />
所谓求之乏力，指的是自由主义者对于思想启蒙民主追求缺乏功利之外的内在动力，容易形成不同的利益小圈子，患得患失，疑神疑鬼，相互攻击，内哄不断；由于对制度改良社会进步缺乏发自内心的持久热情，在缺乏名利剌激的情况下，容易产生搭便车、看热闹心态，在遭遇挫折和危险时则容易产生“跑跑主义”。<br />
<br />
所谓“启”之无道，指的是自由主义者启蒙民众的方式方法和思想深度都有问题。<br />
例如，恶意贬斥、盲目反对中华文化，本身就是一种文化蒙昧，凿丧和断绝了民主的文化之根、传统之水。作为启蒙主体的中国自由主义群体本身仍有待于道德及文化方面进一步的启蒙。<br />
<br />
民主追求是一种特殊的公益事业，需要相当强烈的利他、奉献、牺牲精神，不能理解和证悟“天地万物一体之仁”、不能认识生命本质者，尽管也可以拥有这些精神，但在主观上、根本上终究有所不足。王阳明在《大学问》中写道：<br />
<br />
“大人者，以天地万物为一体者也。其视天下犹一家，中国犹一人焉。若夫间形骸而分尔我者，小人矣。”(《大学问》，《全书》卷二十六)。<br />
<br />
儒家亲亲仁民爱物的“根”、救世利他精神的“”扎在这里。“以天地万物为一体”，想不爱人爱物、利他利世，不可得也。以王阳明的标准衡量（这也是儒家最高标准），世人大多属于“人之小者”，自由主义群体也不例外，自由主义不能识自本心，自然属于“小人之学”。注意，这里的小人不是一般意义上的道德判断，而是站在儒家立场上对自由主义实事求是的评价。<br />
<br />
这么说，并非否认自由主义，相反，我们认为相比其它各种“主义”如马列主义、韩非子主义、神本主义来，自由主义无疑具有颇高的文明性、先进性、真理度，特别值得儒者汲精取华，只是它相比内圣外王一体的儒学而言，有所不足罢了。<br />
<br />
三<br />
儒家在义理上唯我独高，但绝不唯我独“正”其余皆邪，对世间其它各种学说信仰都会给以如实的评价、一定的尊重和不同程度的认可。另外，真正的儒家必定坚持真理至上，良知至上；真正的儒家立场，乃是符合仁义原则和中庸之道的立场，也必是独立、自由的。<br />
<br />
明儒吕坤语：“广所依不如择所依，择所依不如无所依。无所依者，依天也；依天者，有独知之契，虽独立宇宙之内而不谓之孤，众倾之、众毁之而不为动，此之谓男子。”说得真好。对于儒家来说，这个所依的“天”，就是自心本性，就是良知。独立，是“立”于良知之上；自由，是由着自心而行。<br />
<br />
多位自由主义大腕表示特别厌恶儒家“为天地立心，为生民立命，为往圣继绝学，为万世开太平”之类狂言妄语中体现的道德傲慢。这种“表示”和“厌恶”，透露的恰是他们自身的文化肤浅和道德傲慢。<br />
<br />
良知仁性，即天地之心，生民之命，仁学（良知学）即往圣之绝学，仁学落实到政治上、开辟出王道，自足以为万世开太平矣。“四为”方针，充分体现了儒家崇高的道德境界和社会理想，也是儒学生命和社会双重功能的如实阐说。其实，民主追求也不失为一种“为生民立命”“为万世开太平”的努力，只是自由主义不识心、不知命、不解圣学，其努力的效果终究有限，所能达到的境界只能局限于王道政治的基础和初级阶段。<br />
<br />
儒家的道路和方向，是人类社会发展、文明进步的最佳道路至正方向。西方制度建设和物质文明阶段性、暂时性的超前，与它们对中华文化学习汲取有关，更与中国的自我摧残自我毁灭有关，所谓此消彼长。随着马列主义的衰退和中华文化的在本土的复兴升级，西方的优势便将逐步消失，如不进一步接受中华文化的引导，西方的没落是迟早的事。2010-1-1东海老人<br />
