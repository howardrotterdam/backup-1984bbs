<br />
┌────────────────────────────┐<br />
│　　　 《浅析自由主义的不足》读后给东海老人的建议 　　　│<br />
│　　　　　　　　　　　　　　　　　　　　　　　　　　　　│<br />
│　　　　　　　　　　　　洪 哲 胜　　　　　　　　　　　　│<br />
└────────────────────────────┘<br />
<br />
东海老人，你好！<br />
<br />
大作《浅析自由主义的不足》接到，谢谢，将发。<br />
<br />
这个标题本身就设定得很好。大概没有人会认为自由主义已臻至善，<br />
毫无“不足”，因此，自由主义者对于这一篇不会一看标题立即怒火<br />
冲天、滋生敌意，而有可能用平常心来开始拜读你的批评意见了。<br />
<br />
如果改用这样的标题，或许你更能吸引眼球，提高被接受度──《自<br />
由主义如果和儒家思想联姻》。因为，这样的标题，首先是在向自由<br />
主义者展显“笑脸”，搞“统战”，而不是要批臭人家、要踢倒他们<br />
再踏上一只脚；不是要说人家的作品一文不值而要“取代”之以自己<br />
的东东；而只是要善意地和它“联姻”、要把它所缺少的一些好的<br />
“基因”通过“联姻”传给它的下一代，让它的下一代成为好的“升<br />
级版”。<br />
<br />
需知，当你如此这般地降低姿势，把自己的说法从“儒家是天下的最<br />
高真理”，在行文口气上降为“儒家是可以用来补充、形容（给自由<br />
主义以“定语”）的东东”时，你就有更大的可能性把自由主义<br />
“儒”化了。这是典型的“以退为进”的好策略。<br />
<br />
另外，有一点写作技巧可能有助于在各个面向推广你的理念。比如<br />
说，你在文章的第一段采用这样的结论：<br />
<br />
　　“自由主义缺乏必要的认知。”<br />
<br />
如果你把它改写成<br />
<br />
　　“自由主义没有儒家的这一考虑，如有，它就可以在实践上避免<br />
　　一些常见的弊端，让自己得以升华而更加精致化。”<br />
<br />
然后，你可以尽情地仔细提供你的意见。这样一来，倾向自由主义的<br />
读者就不会从一开始就心存芥蒂、既防备又排斥地阅读大作，因而就<br />
有了好奇心可以试着敞开心胸倾听你的批评意见、并对自己的自由主<br />
义思想开始进行反思：儒家的那些老东西竟然也会有东海老人所说的<br />
那样有用的功能吗？这会是真的吗？？<br />
<br />
这时，你有需要小心地提出自由主义有需要儒家润色的地方。它们首<br />
先必需千真万确地是自由主义理论本身所具有的弱点。千万不要把某<br />
些自由主义者对于自由主义的“误解”或“私解”所导致的弊端也列<br />
入，免得让一锅好粥里有了几粒鼠屎，减少你的说服力，甚或节外生<br />
枝、引发敌意。<br />
<br />
我从事了40多年的“企图说服别人”的工作，上面所提到的小小策<br />
略，乃是经验所得的一个非常有用的“常识”。如果你也能够用到，<br />
那就太好了！<br />
