													<h2>王道政治与民主制度</h2>
						
						
																											<div id="postmessage_414961" class="t_msgfont">王道政治与民主制度<br />
<br />
儒家讲内圣外王，圣者尽伦，王者尽制；圣是内在修养，王是外在功业；正心诚意修身，最高境界为圣，齐家治国平天下，最高境界为王。天下为家的君主制度所追求的是小康理想，适合据乱世和升平世，代表了儒家政治现实主义的一翼；天下为公选贤与能的王道政治，适合太平世，指向大同理想，代表了儒家理想主义的一翼。<br />
<br />
王道通三。王道政治要具备三重合法性基础，即超越之基础（天道的合法性）、文化之基础（传统的合法性）及民意之基础（民意的合法性）。蒋庆先生在《政治儒学》中曰：<br />
<br />
“以‘天下归往的为民思想’来确立政治秩序合法性的民意基础、以‘法天而王的天人思想’来确立政治秩序合法性的超越基础、以‘大一统的尊王思想’来确立政治秩序合法性的文化基础。无论古今中外，凡政治秩序欲合法，必同时具有此三重合法性的基础，即必同时具有民意（世俗）、超越（神圣）、文化（传统）的合法性基础，缺一必不能合法。<br />
<br />
钱穆先生也说过类似的话，认为“较之中国文化传统中之治平大道”，“近代西方民主政治，仅尚社会一时多数人意见。而并无超社会以上更高一层之规矩道义可言。”<br />
<br />
确实，儒家王道政治超越了西方民主的功利化平面化特征，具有无可比拟的优越性。但是，陈义过高远离现实，以此为由拒绝和批判民主，令人想起“涸辙之鱼”的寓言。连民意的合法性都不具备，奢谈什么“更高一层之规矩道义”？<br />
 <br />
老枭以为，三重合法性中，民意基础是现代政治最重要、最根本、最关键的基础，是基础之基础。当务之急是以制度的先进来体现政治秩序的民意合法性。“天视自我民视，天听自我民听”，超越的合法性与民意的合法性在一定程度上是重叠的（重叠而不相同。到目前为止，民主是各国内部的民主，一国之民意与全球之民意、各国国民之短暂利益与全体人类之长远利益并不完全一致）。民主化之后，民意的合法性可以通过这一制度得到量化的体现，在这一制度上，其余两重合法性也更有机会、更容易获取。<br />
<br />
在民意的合法性这一点上，王道政治与自由主义要求不约而同。此外，重视典章制度的改良创新的儒家外王学说，也可以成为当今政治改革的重要思想资源。所以我认为，在道德方面，儒家学说与自由主义不妨互补；在政治方面，儒家学说与自由主义可以相契。2006-6-19东海一枭<br />
