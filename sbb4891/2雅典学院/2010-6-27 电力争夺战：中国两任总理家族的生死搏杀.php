													<h2>温家宝与李鹏家族博杀电力</h2>
						
						
																											<div id="postmessage_707714" class="t_msgfont">作者：秦汉 旁观中南海系列<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp;&nbsp;&nbsp;　　温家宝撰文纪念胡耀邦，被海内外高调解读为温曲线表达政改意愿的信号。但知情人透露，温此举实有另番用意：借民间的“六四”记忆树立自己打击李鹏，为在已经开始的电力争夺战中抢得民意先手。 (博讯 boxun.com)<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 自中共改革以来，红色家族经30年运作，已成为左右中国政经大局的一股主要力量。中共高干家庭利用手中的政治权力，瓜分和控制国家重要行业，如江泽民家族控制中国电信行业，曾庆红家族、周永康家族控制石油能源行业，李鹏家族控制了电力行业，朱F基曾一度控制金融行业，温家宝家族控制了珠宝行业。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 但据英国《金融时报》分析，随着2002年开始的权力交替，江泽民之子江绵恒和朱F基之子朱云来的影响力在消退，而更为年轻的一代太子党开始呼风唤雨。温云松和他旗下的新天域资本是更为强势的太子党的“红小兵”，在中国本土兴起的私募股权投资基金行业里占据着主导地位，通过瓜分国家资产和为私有公司提供融资获取暴利。《金融时报》报道，新天域资本公司(New Horizon Capital)，正在募集一档10亿美元的私募基金，将投资准备股票上市的大陆龙头型企业。据报这将是新天域资本公司于2007年成立以来，所募集的第三个，而且是规模最大的基金。目前该公司管理的资产规模达5亿美元，先前曾参与提供资金的外国企业包括孙正义旗下的日本软件银行，以及新加坡总理李显龙夫人何晶主掌的主权财富基金淡马锡。报道认为，温云松作为一个毫无资本运作丰富阅历的年轻人，之所以能吸引国际资本的强势加入，正是因为他手中最重要的资本――其父，当今世界最有权势的人，中国总理温家宝。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 以温云松为代表的新一代太子党“红小兵”，完全不同于朱云来等上一代太子党，“以前，这些有背景的人士最好的选择是去高薪的西方投资银行，但是现在经济实力发生了改变，”一位因话题敏感而要求匿名的业内人士向《金融时报》透露，“现在他们跟那些外国人说，嘿，我现在有权了 ，生意都在我这儿，所以你们把钱给我，我自己来投资，还要占大头哦”。一些分析人士和业内人士预测，那些权贵家族的子孙们会利用私募股权投资基金行业来分享经济利益，牺牲者不但是外国投资者，也包括和中国的革命政权建立者有着血脉关系的“老一辈”太子党。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 知情人士透露，《金融时报》的分析可谓一语中的，但“红小兵”们的欲望远不止此，近年来连续的电力改革，正是温家宝争夺电力系统掌控权，觊觎和抢夺李鹏家族掌控下的电力行业的重要手段。现在温家宝与李鹏两大家族已经撕破脸皮，公然开战。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 温家宝对电力蛋糕觊觎之久<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 　　李鹏家族掌控中国电力行业已非一日，这在中国已经算不得秘密。其子李小鹏，原是华能集团的老总，掌握华能集团多年。自2008年5月后离开华能，商而优则仕，转任山西省委常委、副省长。2010年6月又升任山西常务副省长，虽然离开电力，但仍然掌握着中国电力的上游命脉――煤炭。其女李小琳，也身为中国电力集团副总经理、中国电力国际发展公司董事长。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 　　除家庭成员外，中国电力系统大大小小的部门、公司，都布满了李鹏的人马。凡数得上的各大电力公司的老总，都与李家有着千丝万缕的联系。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 温家宝对电力觊觎之心已久。他妻子张蓓莉已实际上掌握了中国的珠宝业，在国际珠宝业也有很高的地位，但这个行业在国民经营中无足轻重，总属微末，虽然可以换得盆满钵溢，家财数亿，但与煤炭、能源一类战略性行业相比，是典型的“边角余料”，难以掌握国计民生的命脉。不掌握事关国计民生的命脉产业，在政治上话语权就不强，即上不得台面，又容易授人以柄，更易被人打压。于是温转而迅速插手金融，让自己的儿子，以私募的方式在命脉产业中打入一个楔子。并且借电力改革手段，在李家的地盘上打起了算盘。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 这就是2010年2月初电力系统大调整的原因。知情人士讲，目前，中国电力行业正在迎来自2002年电改以来，最为密集的一轮人事变动，首当其冲的就是李鹏人马。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 知情人士讲，温对电力系统下手，真可谓高瞻远瞩，步步为营，杀法绝断，手段老道，端得是即稳又准还狠。与之相反，李鹏则节节败退，顾此失彼。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 温氏阳谋：步步为营，化整为零，分而治之<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 温能做到这一点，是因为他前期就铺排有方。自十六大上台后，温即策动电力改革。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 首先是分而治之。2002年，温以“在发电环节引入竞争机制”为由，借反垄断推动电力改革。推行“厂网分开”，将国家电力公司管理的电力资产按照发电和电网两类业务进行划分。发电环节将国家电力公司管理的发电资产直接改组或重组为规模大致相当的5个全国性的独立发电公司。电网环节分别设立国家电网公司和中国南方电网有限责任公司。国家电网公司下设华北、东北、华东、华中和西北5个区域电网公司。温为赢得电力行业精英的支持，强调上述新设公司或机构均普遍实行“一把手”管理制度。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 虽然李鹏对电力行业一言九鼎，但这一改革得到了电力行业的普遍支持。一则，原来只一家国家电力公司，位置有限，李鹏手下全挤在那儿，得不到升迁，一旦有了宁当鸡头不为牛后的机会，谁不愿意？二则自原国家电力公司老总高严事败出逃后，李鹏深受打击，他也不愿意再犯这种把鸡蛋放在一个篮子里的错误，也愿意让自己的手下人多占些山头，多些回旋的余地。虽然心有不甘，也只得同意。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 这就为温分治电力行业提供了条件。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 其次是内部分权。实现第一步后，温在2007年开始逐步对电力行业进行人事调整。其时，中国电力投资集团公司总经理、党组书记王炳华被调往刚刚挂牌成立的国家核电技术公司担任董事长；原国家电网公司副总经理陆启洲接任中国电力投资集团公司总经理、党组书记。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 2008年4月，国电集团原总经理、党组书记周大兵退休，朱永M接任国电集团总经理，李庆奎任国电集团党组书记。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 2008年7月，原国电集团副总经理李庆奎担任华电集团副总经理、党组书记一职；原中国电力投资集团公司党组成员纪检组组长乔保平调任国电集团担任党组书记、副总经理职务。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 2008年5月，工业和信息化部副部长王旭东接替尤权出任国家电监会主席。这王旭东也是一波三折的人物，原是“小木匠”李瑞环的人马。1993年由天津市委副书记升任中央组织部副部长，但自此仕途转缓，一干就是7年。直到2000年，才担任河北省委书记。李瑞环一下台，王旭东失去靠山，受政敌攻击被调查，被迫让出诸侯大位，转而到信息产业部降格使用，担任党组书记和副部长。因是内阁成员，有机会与温走得近，在向温表示了效忠之后，得到温的力保，终于摆脱困境，2003年担任信息产业部部长、党组书记。2008年信息产业部改组为工业和信息化部后，温调任他担任国家电力监管委员会主席、党组书记，秘密受命为温争夺电力天下服务。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 在上述人事调整中，温以“优化企业的治理结构”的名义，推翻自己2002年“普遍实行‘一把手’管理制度”的承诺，通过国资委不再让各电力公司的“一把手”身兼多职，而更普遍地设立国企董事会制度，让董事长和总经理“分权”。进一步稀释李鹏马仔的权力。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 电力高层换届调整的同时，温家宝也加强了对电力系统的审计。国家审计署重兵进驻国家电网公司、南方电网公司下属的18个网省公司，同时对中国大唐集团公司、中国国电集团公司、中国电力投资集团公司和中国华能集团公司进行审计。这招杀威棒，使坐在金钱堆上的李鹏旧部人人胆寒。中共官僚，在看风向、排班站队上，个个聪明，于是一些见风使舵的人开始向温靠近。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 嗅出异常的李鹏震怒，但此时已无人可靠。因为那些经他直接提拔的马仔也都年事已高，被逐步逼向二线，而那些奉行“县官不如现管”的电力新锐们更不把过气的李鹏太当回事，纷纷想尽办法向温示好表忠心。无奈之下，李鹏只好亲自出面，向温提出，要求李小鹏从政。其意是希望通过李小鹏向政坛高位进军，以保李家的未来。温果然同意，2008年6月2日，原华能集团党组书记兼总经理李小鹏告别了华能集团，出任山西省副省长。在华能集团的负责人调整之后，电力行业人事调整已经过半。紧接着温通过人事调整，兵不血刃地得到了华能集团，李家江山失去重要半壁。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 再次是全面进攻。进入到2010年1月，温再一次吹响向电力行业进军的号角，对维持了近8年的电力企业格局，借新老交替之际进行电力“掌门人”大换班。此次调整主要集中在中国大唐集团公司、中国国电集团公司和中国南方电网有限责任公司。其余主要的电力集团也多有涉及。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 如，大唐集团总经理、党组书记翟若愚正式退休，大唐集团副总经理刘顺达接任董事长职务；国家电网公司副总经理陈进行调任大唐集团，担任总经理职务。南方电网党组书记、董事长袁懋振退休，原总经理赵建国接任其董事长职务；大唐集团副总经理钟俊调任南方电网总经理职务。中国国电集团公司副总经理陈飞，也调任中国长江三峡集团公司任总经理一职。三峡公司党组书记、总经理退休，原副总经理曹广晶升任三峡公司董事长。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 温氏阴谋：收降纳叛，入股电力，边攻边固<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 与此同时，温又通过两手，将电力行业逐渐掌握在自己手中。一是收降纳叛，派出自己的人马掌控各大电力公司，二是通过儿子的私募基金入股电力，化公为私。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 先说收降纳叛。比如，借几大国有电力公司大换血之际，温的马仔们纷纷抢滩。如中国国家电网公司总裁刘振亚，华润电力控股有限公司总裁、首席执行官王帅廷，新任华能电力集团总裁曹培玺，新任中国长江三峡集团公司陈飞、中国电力投资集团公司一把手陆启州等人都先后离开原来的主子转投温家宝门下。其中又以刘振亚的风头最为强劲，据传马上将接任电监会主席。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 关于刘振亚，后文还有详述，这里先简单介绍一下。刘振亚出身于山东电力系统。从技术员一步步“科技兴邦”到山东电力集团公司董事长兼山东鲁能集团董事局主席，开始了他人生的大跃进。刘用手中资源投靠曾庆红，曾提拔他为国家电力公司副总经理、党组副书记。十六大曾退休前，刘振亚暗中即弃弱投强，转投到温家宝门下。其时温内心已在盘亘如何抢夺电力这块大蛋糕，当然乐得有人作内应，于是任命其为国家电网公司筹备组副组长，主管电力改革。一时在电力系统权倾朝野。因为替温家宝收降纳叛做了大量工作，温提拔他当上了国家电网公司总经理、党组书记，中共十七大时又赏他一个中央候补委员的待遇。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 在温的支持下，被称为“另一个三峡”的巨大昂贵工程特高压项目，在刘的主导下终于力排众议，一路绿灯开始启动。2009年首期投资即高达830亿元，成为温营造新电力势力的“新三峡工程”。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 王帅廷从严格意义上来说，不算是李鹏人马，他走上电力之路也是阴差阳错。王本是江苏省委办公厅的一个秘书，后任徐州市人民政府办公室副主任，由于仕途多阻，1994年徐州成立华润电力有限公司后，他以徐州市人民政府副秘书长身份兼任华润总经理、党委书记，后来干脆辞去政府副秘书长职务，专心从商。当时在中国搞电力，不拜李鹏的码头是不行的，于是王千方百计向李家靠拢。但无奈华润刚起步，在中国电力行业中排不上号，李家也对他不冷不热，就算真心投靠，也顶多算个边缘性人物。2001年华润电力成立后，王任华润电力的总经理，7年只前进了一小步，而且只是名头比以前响亮了一些而已，可见其处境。电力改革一启动，王闻得风声知道温用人心切，于是拜温为主，成为温的马仔。现任华润（集团）有限公司副董事长，兼任华润电力控股有限公司副主席和首席执行官。正蓄势待发，成为华润（集团）总裁宋林的备胎。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 曹培玺则是跟刘振亚一起投向温家宝的。曹培玺是刘振亚一手提拔起来的。1995年刘任山东省电力工业局局长兼党委书记后，就把曹从山东青岛发电厂厂长提升为自己的助理，第二年就提曹为自己的副手。刘升迁至国家电力公司主管电力改革后，将原国家电力公司部分企事业单位合并组建成中国华电集团公司，调曹担任该公司党组成员、副总经理。2006年又提曹担任华电集团党组书记、总经理，华电国际电力股份有限公司董事长。2008年电力人事大调整，中国华能集团公司原总裁李小鹏转任山西省副省长，本该由李小鹏的副手黄永达接任集团总裁，但在温授意、刘振亚的运作下，黄在集团对外发布代行集团董事长之职6个月后，转任集团书记虚职，将总经理之职拱手让给空降而来曹培玺。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 陈飞虽然年轻，但在电力行业却资历颇深，也曾深得李鹏家族信任，一度担任三峡工程施工指挥部指挥长。但由于电力行业当时呈垄断状态，位置有限，像陈飞这样的电力新锐很难跻身上位，受到排挤。在被迫远走广西，担任龙滩水电开发有限公司总经理不久后，转而从政，挂职到云南，后任云南省计委副主任。2002年电力大改革，经人推荐，作为李鹏电力势力边缘被排挤的人物，进入温的视线。正郁郁不得志的陈飞，突然得到天顾，于是感激涕零，表示誓死效忠温家宝。2003年12月温授意国资委，让其担任中国国电集团公司副总经理、党组成员，2006年10月至2008年2月还兼任国电电力发展股份有限公司党组书记、总经理。2010年1月温向电力系统发起总攻后，受温重托担任中国长江三峡集团公司董事、总经理、党组成员。另外，据知情人透露，陈飞还有另一个秘而不宣的使命，就是利用他担任全国青年联合会常委、中央企业青年联合会副主席的身份，为温家寻找、建立和培养青年企业家团队，以确保温家宝对中国政治经济的常久影响力，确保温氏家族的政治、经济利益。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 陆启洲也是个老电力了。出身于地方电力系统，最终以湖北省电力公司党委书记、总经理的身份，上调国家电力公司担任发输电运营部主任，主管发电和供电这个中心环节，对各大发电公司和电网情况了如指掌。刘振亚到国家电力公司后，两人结成同盟，在刘负责电力改革期间出力不少。刘到国家电网公司后，将其调入，任为副手。并通过刘得到温的赏识，2007年进一入高升为中国电力投资集团公司总经理、党组书记。陆启洲这个人工作能力强，而且深谙官场之道，从不得罪上司，是个永远令上司放心的人物。他能到中电投，也是因为其前任王炳华的力荐。2001年9月陆就曾接替王炳华同志担任国家电力公司发输电运营部主任的职务，现再次接替王炳华担任中国电力投资集团公司总经理、党组书记。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 刘振亚为何总是屹立不倒呢？<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 再说化公为私。说起这事儿，还得提刘振亚。这刘振亚何以得到温的信任呢?<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 知情人说，在李鹏家族全面掌控电力的时期，刘振亚实际上被视为电力的一个异数，因为他不仅不是李家的人，还与李家不对付，得罪过李小鹏和李鹏。但就是这么一个人，居然最终成了中国电力的柱梁，这中间的水可就深了。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 2007年1月《财经》杂志曾曝光“山东第一大企业鲁能集团悄然改制，由两家民营企业受让91.6%股权”一事，民间资本以38亿要鲸吞700多亿的国家资产，一时间天下哗然，刘振亚首当其冲。有人爆料说，这笔交易的背后曾庆红家族、俞正声家族和王乐泉家族。但据知情人讲，其实只是曾家公子和温家儿子所为。原因是刘振亚即不想得罪旧主人，又想得到新主子的欢心，于是三人暗中操纵了这笔交易，无奈事关重大，眼红者众，走漏了风声，被捅了出来。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 事败之后，中纪委介入调查。由于事关温云松，温家宝强力干涉，在全部事实基本查实的情况下，中纪委暂停了对刘振亚本人的调查，鲁能的私有化也停了下来。然而，据知情人讲，2009年一审计，鲁能资产却又少了两百亿，原来刘振亚既然不能够一次性“批发”，就干脆“零售”，这三年把鲁能的资产零零星星低价卖给了两位公子的多位代理人，以换得保护伞。中纪委震怒，欲再次对刘启动调查，但受阻于温，温竭力死保。同时温全力运作，欲将刘提拔为电监会主席，以突破中纪委的围剿。此事因为查有实据，温的死保引来中纪委很多人的不满，大家都是靠政绩吃饭，你保住一个，中纪委这边的政绩就少了一块。负责此案的中纪委第一纪检监察室，是中纪委二把手何勇的嫡系人马。这让何勇十分恼怒，曾私底下气愤地誓言，一定要将刘振亚关入监狱。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 但何勇的誓言也顶多是一个行将就木的老人的怨言，早已过了退休年龄的他，十八大肯定要下台，届时温的人马掌控中纪委，刘振亚终会逃出此劫不说，那些得罪了温家的何勇旧部，也将面临被清洗的危险。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 另外，据知情人透露，刘振亚还傍上了中纪委书记贺国强。刘与贺，从渊源上看，本应是政敌。因为贺国强是李鹏的人马，刘振亚曾在国家电力公司原总经理高严的问题上，揭发高严以自保，有一些涉及到李小鹏，得罪了李鹏家族。为了向贺国强示好，刘振亚正主动提出要调贺的弟弟贺锡强担任自己的副手。贺锡强1959年生，先后担任湘潭电业局局长、湖南省电力公司副总经理、华东电网公司副总经理、浙江省电力公司副总经理兼党组书记、上海市电力公司副总经理兼党组书记，现任华东电网公司总经理兼党组书记。有了贺国强这个靠山，中纪委再想动刘振亚，就更困难了。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 温云松私募基金大举入股电力项目<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 除了刘振亚这一支，温还亲自上阵，为儿子抢夺电力及新能源冲锋陷阵。一方面，温让自己的马仔，国家发改委副主任张国宝担任能源局局长。张1944年出生，按理早该退休，但温不顾别人反对，强行让他超期服役，就是要让张扶温公子一马。温通过张国宝，让温云松入股大唐电力的煤化工项目，未来上市套利也在数亿之巨。另一方面，温亲自担任新设的国家能源委员会主任一职。而这个职务此前舆论普遍认为是由副总理李克强担任。海外评论认为，“这个底牌一揭，国内媒体的颂歌之中，也普遍暗含对于权力游戏的惊叹”。凤凰网评论认为，“国家能源委员会简直就是一个国家安全委员会”――而这是江泽民当政时，一直想抓在自己手里却没有抓到的。如今温家宝举重若轻，以区区的能源名义，就实际上掌控了国家安全的大权，温以实际行动为自己的“世界权力第一人”做了最好的注脚。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 在温主导下，温云松的私募基金频频斩获大单，如先后入股大唐国际在内蒙古赤峰和大连华锐的风电项目等等。其中大唐赤峰克什克腾旗大黑山风电厂首批风机2007年12月并网发电，成功并入东北电网。大连华锐风电科技（集团）股份有限公司，2008年新增风电装机容量1403MW，行业排名中国第一、全球第七；2009年新增风电装机容量3510MW，行业排名中国第一、全球第三。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 温家宝的欲望远不止此。知情人称，新天域还欲收购中国银行（国际）51%的股权，但遭到中银国际董事长肖钢的拒绝。看来，在争夺到电力蛋糕之后，金融业成为温家宝要吸引消化的另一个战略性行业。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 电力行业在温手中，或可会演化为中国多党民主制的资本<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 　　知情人称，温时常畅谈政治改革，明言普世价值，还撰文纪念耀邦，很多人因此认为温是个民主改革派。其实，温既不是民主派，也不是保守派，就是个投机性极强的人。三个性格迥异的总书记，他都服侍得他们极为舒服，就连帮派心里很强的江泽民，也被温哄得团团转，扶持他当总理。这样一个大内宦官性质的人，不可能有什么主义，理念，唯一的信念就是怎么能当官就怎么做，怎么能获取最大利益就怎么干。如果形势倒退，要他当金正日，他绝不推辞。如果民主大势昭然，要他登高一呼，甚至组党竞争当中国新国父，他也一定会干。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 他既可能是叶利钦，也可能是亚纳耶夫（苏联819政变头头，被推为代总统），但他绝对不是戈尔巴乔夫――他既没有戈尔巴乔夫那样坚定的信念，也没有一以贯之的政治追求。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 知情人称，此番电力大战，或许会结出另一类果实，假使电力行业到了温家手中，或许以温云松为代表的新一代“海归太子党”，会将这部分资源，运用到推动中国民主的事业中。因为温云松才30多岁，比起前代太子党仗恃父辈闹革命打天下的资本，身披革命出身的“根红苗正”，温云松他们这代人，少年留洋，身心俱受民主洗礼，继承的却是民主的“根红苗正”，知道民主是大势所趋，若用这些资源为中国的多党制培根养基，也是善莫大焉的一件事。<br />
