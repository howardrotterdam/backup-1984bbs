																											<div id="postmessage_284151" class="t_msgfont">『关天茶舍』 [亚洲论坛]龙女士的自我推崇与家国想像 <br />
 作者：玛特 提交日期：2006-2-15 13:58:00 访问：125 回复：0&nbsp;&nbsp;<br />
<br />
??就“Ice Point Weekly”（以下简称IPW）被封，龙ying台致书“锦Tao先生”，信中以第三人称提到：“刊出龙ying台的《你可能不知道的Taiwan》时，网路上已经四处流传‘IPW’被封杀的臆测。”原来，龙女士刊发该文后，已“在等着鬼的半夜敲门索命”一般，期待着这一富有美感的仪式之完成。可恨某党颟顸延宕，最终竟以袁 WS先生一篇在对岸只堪目为“常识”的文章，当作“封杀”的“理由”。<br />
　　当初拜读《你可能不知道的Taiwan》，我已暗地惴惴于自己的疏陋。原来台湾的民主化进程，除了公认的“美丽岛事件前后”、“解严前后”、“政党轮替前后”的分期，还有一个“野火之前”与“野火之后”的坐标。但仔细一想也就明白了。龙女士的自我期许，仍不脱传统士大夫“立德、立言、立功”的路径。挟《野火集》的煌言与“北市文化局长”的事功，龙女士所缺的，仅是一顶“华语文学界的良心”之类的桂冠了。她从台湾到新加坡、香港，一路与风车作战，直逼大陆，所图者盖此。<br />
　　问题是，龙女士担当得起她的这种自我期许吗？<br />
　　致“锦tao先生”书，其论述所指向的，显然不是收信者本人，他只会在眼镜后面挂着格式化的微笑，置之不理，这一点以龙女士之明，不会不察。那么她无非是以一个“意见领袖”的姿态，在向“我们暂且不管…怎么看”的“大陆的知识分子和一般人民读者”宣教。可是这番宣教中，包含了诸多误判：<br />
　　首先，“IPW”“仅有的、被割断的喉咙”的谥号，是不能私相授受的。龙女士已经体察到“二十一世纪…地球村”的天下大势，想必对“喉咙”与“声音”不止限于纸面媒体也了然于胸。网路上的资讯是无法绝对屏蔽的，否则我也就无法拜读及回复龙女士的大文。而“宣传、舆论”本来就是某党视为“阵地”务须“占领”的，“IPW”作为“团*中*央机关报”《中@青@报》的副刊，原本就在体制之内，而不曾逾于其外，若与上峰“精神”有所I格，被消音也是题中应有之义。正如不同意“野火前后”的分期，我同样不能苟同“IPW前后”的分期。如果因为IPW”曾刊登龙女士的大作而过度拉抬它的道义标高，那是昧于私义、见事不明；<br />
　　其次，对大陆知识分子的整体判断，也有待商榷。知识分子不仅限于庙堂、象牙塔与艺文圈，网路上、各行各业中，象笔者这样的“知道分子”、“识字分子”、“业余知识分子”在所多有，我们一样汲汲于新知、真知，一样禀有龙女士那些并不新鲜的“价值认同”，不需要龙女士俯视的视角与“常识”的普及。龙女士所谆谆宣教的，我们早已知晓，许多人且已在践行。我们无法接受龙女士对大陆知识分子的低估、蔑视与狂妄的自我推崇；<br />
　　最重要的，乃是我们有异于龙女士的家国想像：龙的信中说，大陆“多少人物欲横流，多少人辗转沟壑”，前一句只要不是出于清教徒的偏执，并没有什么稀奇；后一句则显然是过度的夸大，不如直接诋为“水深火热”。龙说，“人们曾经对年华正茂的您寄以期望”，这只是留于“企盼明君”的层面，事实上我们探讨更多的是制度的建设。近两三年，因孙zhigang案推动的立法改进、“宝马撞人”案引发的民意浪潮、以及“李shangping被害”案仍持续不懈的追究，还有举凡拆迁、物业管理等事件的抗争，都显示“大陆的知识分子和一般民众”的自觉争取及获得的点滴改进。台湾的民主化历程，也是如此筚路蓝缕、一路走来。“在路上”并不能成为被轻视的理由，两岸之间也没有“文明与野蛮”的绝对落差。在我们眼中，“中国这片深爱的土地”的前途并不因区区“BD”一事更形黑暗，而仍是值得“无条件地执著”并必将日趋光明的。我们不是任由“锦tao先生”“以什么手段去对付”的“人民”，因此也不需要一个“龙应台”这样的广大教主。<br />
　　而基于这种错误的“家国想像”，龙女士不讳言她在“价值认同”与“家国认同”之间的机会主义态度。一旦哪一边“不符合我的‘价值认同’”，她“离之弃之抵抗之”的对象，究竟是“执政党”呢，还是“家国”本身呢？似乎是后者，因此我们可以视之为一种逃脱。当然了，龙女士本就是依违游走于两岸三地之间，见机而作的。“可怜一个云中鹤，飞去飞来宰相堂。”问题是，这种大言不惭的暧昧身段，如何能获得我们的“认同”呢？<br />
　　又要扯到“团团”、“圆圆”了。据我的印象，熊猫作为“友好使者”，去美国的已有多年且不止一对。我不否认熊猫在某种意义上是“统*战*猫”，但也没见美国的对华政策有任何程度的因熊猫的功效所致的松动。民#进#党只知坚拒的态度，还不是换个方向落入“统战”的算度之中？而龙女士在多大程度上能代表台湾民众“在乎‘IPW’安危”而“不在乎团团和圆圆来不来台北”？我也深深存疑。至少，据我所知，这种“IPW绑熊猫”的搭配，在岛内舆论，也是仅见。<br />
　　说到底，在过高的自我推崇和错误的家国想像之下，龙应台名为致“锦tao先生”的信，只是作给“野蛮”对岸被当成“奴仆、家仆”使用的待启蒙的知识分子看的一个严正姿态罢了。我们见得事多。此举固然可恶，也不必如此小题大做吧？以一“IPW”而取舍熊猫乃至取舍“统一”，龙女士谬之深矣。</div>

							
							
							
							
