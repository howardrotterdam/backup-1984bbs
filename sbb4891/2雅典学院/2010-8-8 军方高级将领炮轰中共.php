据香港星岛日报报导，刘亚洲最近接受香港媒体专访时发表番骇世之言。该专访前半部主要论述中国西部所面临复杂的国际环境，纵谈中国的西进战略。但在专访的后半部，这位“将军作家”对境内时政发表看法，言辞观点令人震惊。刘亚洲直言一个制度若不能让公民自由呼吸、不能把代表人民的人放在领导岗位将“必然灭亡”；他甚至预言中国在十年内，必将向民主政治转型，称这是不可避免，因为“不可能有退路”。<br />
<br />
金钱外交严重影响中国国际形象<br />
<br />
刘亚洲首先炮轰中共流行的“经济强国”和“金钱外交”的思想，指“钱多不意味软实力有提升”；他以中国在非洲投资大肆行贿、用金钱开道为例，指此举不但使非洲官员胃口越来越大，也让当地民众对中国政府、中国企业极为反感。<br />
<br />
“除了金钱的力量，我们还有甚么力量？”他指出，“今天的中国社会，令人担忧的一个现像是，从上到下都洋溢着有钱好办事、钱能摆平一切的热情。它带来的是急功近利、用钱买路的逻辑和行为方式，完全忽视必不可少的长期细致工作，以及自我形象的提升。”<br />
<br />
刘亚洲指出，“单靠金钱的力量不但无法长期保有中国在境外的国家利益，甚至也不能保有境内的平安稳定，迷信金钱力量的民族是落后和愚昧的民族，无论是用于内政的安抚，还是世界的拓展，而拥有经济和文化双重优势的民族，才是真正强盛的民族。民族的竞争是全方位的竞争，是综合素质的竞争，确定民族命运的决不仅仅是军事和经济力量，而主要取决于文明形式本身，因此中国必须进行政治体制改革。”<br />
<br />
预言中国十年内必将向民主政治转型，“不可能有退路”<br />
<br />
刘亚洲以前苏联比喻，指苏联的失败主要在于内因，它并没有被世界大战打垮，却在制度竞争中被打垮了，苏联的教训都是中国的镜子。“一个制度如不能让公民自由呼吸，并最大程度地释放公民创造力，不能把最能代表人民的人放在领导岗位上，就必然灭亡”。他更直指，当年的苏联也曾强调稳定，把稳定看成目的，“稳定压倒一切，金钱摆平一切，结果激化了矛盾，一切反了过来”。他强调“民族的生存，决定我们必须进行政治体制改革，人类社会以十年为度，可见政治兴衰；以百年为度，可见社会兴衰；以千年为度，可以看到文明的兴衰”。他并预言，“十年之内，一场由威权政治向民主政治的转型不可避免的要发生，中国将会出现伟大的变局，政治体制改革是历史赋予的使命，我们不可能有退路。”<br />
<br />
“美国成功的秘密不在于华尔街，也不在于矽谷，而在于长盛不衰的法治和法治背后的制度。”刘亚洲直指，只有制度可以信赖。刘亚洲表示，中国并不缺乏真理，而缺乏容忍真理存在的土壤，“没有民主，就没有持久的崛起。民主思想传播不受国界限制，也不受历史限制。”<br />
<br />
