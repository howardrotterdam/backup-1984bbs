行政令10999：允许政府征用所有类型的交通工具。 EXECUTIVE ORDER#11000 allows the government to mobilize civilians into work brigades under government supervision.<br />
行政令11000：允许政府动员公民在监管下组队劳动。 EXECUTIVE ORDER#11921 Provides that the President can declare a state fo emergency that is not defined, and Congress cannot review the action for six months.<br />
行政令11921：总统可以不经解释宣布国家进入紧急状态，国会6个月内不得对行动做评论。 Senate bill #1873 allows the government to vaccinate you with untested vaccines against your will.<br />
参议院法令1873：允许政府违背你的意愿，向你注射为验证的疫苗。 The FDA says Americans do not have a right to know which foods are genetically modified.<br />
FDA说美国人没有权利知道哪些是转基因食物。 Congressman Sensenbrenner's bill(HR 1528) requires you to spy on your neigbors, including wering a wire. Refusal would be punishable by a mandatory prison sentence of at least two years.<br />
国会1528法案:要求你监视你的邻居，包括安装窃听器。拒绝者将会被判处至少2年刑期。 The government claims the power to seize all financial instruments: currency, gold, silver, and everything else if they deem an emergency exists.-----Treasury Department Letter August 12,2005<br />
政府宣称有权收缴一切金融工具，包括，货币，金银等，只要他们认为存在紧急情况。还有很多，不一一列举了。 911之后权力大增的小布什说，宪法只是一张该死的纸。他说的很有道理。<br />
看到这些法律，我的毛也慢慢的竖了起来，借着反恐的名义，美国越来越中国化了，甚至很多都是天朝都没有开发的品种，他们就明目张胆的写在法律上，国会6个月内不得对行动做评论更是莫名其妙方便少数人犯罪。<br />
对美国，我的感情说不上喜欢或者厌恶，但美国的政治制度一直是我欣赏和敬佩的，那群国父的立宪精神尤其让我神往，但看到这些年的这些越来越多的限制自由的法律，越来越明目张胆的少数利益集团，也渐渐地感到忧虑。这一代的美国人不应该是只顾享乐和疯狂消费，最终掉入金融陷阱的群体，他们应该懂得如何阻止这些限制自由的发生。 也提醒我们中国人在推动自己国家政治改革的时候，也要引以为鉴，不要刚跳出少数政治精英分子的控制，又陷入另一撮儿经济精英的陷阱。<br />
也告诉我的那些朋友们，无论你是男是女，在这个现代化的强制性和规范性疯狂吞噬自由的时代，都必须学会了解一些政治，这个时代没有隐士，也没有谁能逃避政治，你不承担起自己的责任，很有可能就会成为电影里面的那些被抄没家产者，你的店铺无法精英，你的女儿无钱上大学，你的房子被强拆。你无须精通，却必须懂的一些常识，在需要的时候，要做出自己的决断。至少要弄明白，谁谁动了你的奶酪。<br />
