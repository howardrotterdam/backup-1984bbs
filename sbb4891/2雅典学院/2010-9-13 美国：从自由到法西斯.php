<br />
(3)应纳税所得额的计算及应纳税额的计算。美国个人所得税的计算步骤是：税法规定的各纳税人的全部所得先减去不予计列所得（为总所得）；然后减去税法规定的应在“调整所得前扣减的项目”（为调整所得）；再减去分项扣减款项或标准扣减款项；再减去免税项目（为应纳税所得额）；乘以适用税率（为应纳税额）；最后减去税收抵免税款和已付税款等，即为最终的纳税款。对每一项目的计算，税法都有详细的规定和计算规则。<br />
<br />
(4) 征收方法。美国个人所得税的应税期限为一全年。特殊情况可少于12个月。全年纳税年度截止于每个日历年12月31日的，为按日历年度计的纳税年度；截止于每个日历年度其他的任何一个日期的，为按财务年度计的纳税年度。<br />
个人所得税的纳税人按日历年度为纳税年度的，在纳税年度次年的4月15日前申报纳税；按财务年度为纳税年度的，应在纳税年度终止后次年的第4个月的第15日前申报纳税；以领取工薪的纳税人，实行由雇主预扣代缴的制度。<br />
<br />
<br />
美国征收联邦个人所得税，除阿拉斯加等7州外的各州还征收州个人所得税。（1）美国个人所得税征收遵循属人原则，凡美国公民，均就其来自美国国内外的所有收入纳税；非美国公民的外国人，只就其来自美国国内的收入纳税。（2）课税范围主要包括：雇员的工资、薪金、退休金；独资或合伙经营取得的商业利润；利息、股息、租金、特许权使用费、资本利得、农业收入及其他收入。（3）联邦个人所得税实行15%-39.6%的五档税率。非居住的外国人在美国取得的与工商业活动直接有关的所得，比照美国公民适用的累进税率纳税；取得与工商业活动无直接关系的所得，除税收条约另有规定外，按30%的比例税率纳税。州个人所得税税率一般为1%至11%。（4）应纳税所得额为纯所得减去减免项目后的余额，纯所得为调整毛所得减去扣除项目后的余额，调整毛所得为总所得减去不予计列项目和经营费用后的余额。总所得中不予计列项目有：资本利得的半数，州和地方政府公债的利息，雇主提供的医药费，教师的讲学金，失业救济金，慈善机构赠予的救济款，亲友馈赠的礼金，战争负伤抚恤费，债务支出、债权收入等。从调整毛所得中可以扣除的项目，分为标准扣除和分项扣除，标准扣除是对单身或夫妻联合申报分别核定一个普遍性的减免额；分项扣除的项目包括州与地方政府的税款、抵押借款、医药费、给慈善机构的捐款、幼儿保育支出、工会会费、意外事故损失、与工商业有关的直接费用支出等。从纯所得中可以减免的项目，包括纳税人个人及其配偶等家庭成员每人每年可享受的3000美元左右的免税额。为鼓励购房，个人购房支出也可在税前扣除。（5）个人所得税采取先按季预缴，再按年申报清缴和部分预扣两种方式。即：职业固定的雇员，其工薪收入的应纳税款由雇主预扣代缴，年终由纳税人申报结算；纳税人的其他收入，按季自报缴纳，年终结算；资本利得税在出售股票、财产取得收入时课征，年终不再申报；外国人在美国按总额单一税率所纳税款在取得收入时预扣，年终不再申报。 <br />
