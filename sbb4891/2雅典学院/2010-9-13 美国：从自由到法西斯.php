公路建设支出来自燃油税。国防支出来自于企业所得税。（原话是：我们的国防支出，恰好等于企业所得税收入。注意，是恰好等于！）<br />
所以，企业所得税不会用来投资教育和建设公路，燃油税和财产税不会用来搞国防。<br />
我们发现，美国搞立法的这种精神是十分值得敬佩的，每一种税收都要有一种合理的解释，也就是你是用来干什么的？有没有这个必要？如果征收了要征收多少？向谁征收？不像我们天朝，每次人大连预算都不公开，每个部门分了多少钱都是秘密！！（记住，是分了，这是国务院内部人士的叫法！！） 但个人所得税就不同了，事实上，美国史1914年之后才征收这项税的，这之前没有这项税收，那么这项税收是用来作甚么的呢？<br />
“所得税的主要目的，不是提高财政收入，而是再分配财富和控制社会。”如果你认为这里的再分配财富是劫富济贫，那你一定错了。大错特错！<br />
事实上，正好相反，是劫贫济富！！ 首先要从为什么要征收这项税开始。这要从1914年发生了什么事说起。1913年《美联储法案》通过，美联储成立！<br />
我们先不说这项法令的具体内容，先说下它通过的过程吧1913年12月22日，众议院以298票对60票通过<br />
1914年12月23日，参议院以43票对25票（27人缺席）通过国会通过了不到一小时，威尔逊总统就签署了这项法令！<br />
熟悉美国社会的朋友应该知道，这几天正是接近圣诞节，以美国国会那种喜欢扯皮的效率，很难通过什么重要的法律，所以很多议员都提前回家过节了（美国议员也当的真实潇洒）所以参议院100人中竟然有27人缺席，个人认为，着27人中的大部分是不同意这项法案的，因为这项法案明显是个阴谋，而支持此法案的参议员们肯定会事先得到消息，而选择留下。<br />
这里介绍一下，在美国，参议员的地位是比众议员高的，可以当六年，而众议员只能当两年，并且参议员每州两人，代表精英，共100人。众议员按人口分配，代表大众，共435人。所以众议员很多人并不是见识热别高的人，容易被糊弄（可怜的威尔逊总统也是被糊弄签署这项法律的，下面再说），而参议员就不同了，很多资深的参议员地位是很高的，一干几十年，影响力巨大。比如去年过世的爱德华・肯尼迪，在参议院一干46年，见证了十个美国总统，最后力挺奥巴马登基（肯尼迪家族是奥巴马当选总统的极为重要甚至是最重要的政治力量），如果不是爱德华肯尼迪的强力支持，奥巴马很可能当不上总统，要推动医改法案通过更是难如登天。难怪他被奥巴马成为“导师”。<br />
说了这么多，无非就是想说，众议员是很可能被忽悠的，总统只有一个，也是可能被忽悠的，但想忽悠这100个参议员，那就难上加难了，所以华尔街的银行家选择了这个当口，趁着大多数参议员度假，国务卿是自己人，总统是个书呆子的绝佳时机，把这个法案通过。很多参议员甚至都没能仔细的看一遍这个法案。（从各种角度看，这都是一个事先早已计划好的阴谋，这群阴谋家的水平之高，技术之高明，实在让人叹服，林biao真的应该好好学学他们） 让我们看看这个法案给了华尔街银行家什么礼物？<br />
美联储，这个控制美元的机构，竟然不是政府所有，而是私人银行！！美国政府竟然没有印美元的权力，而必须向美联储借美元！！这意味着什么，可能你还不明白。<br />
首先，那群银行家掌握了印钞机，他们想要多少钱就印多少钱，美国要发展经济，必须要更多的货币，自己不能印，只能向美联储借。但借了要交利息。这利息从哪儿来？ <br />
就是美国公民的个人所得税！！！美国所有公民的个人所得税是不是用来投资教育，建设国家，甚至用来向伊拉克打仗，而是落入了华尔街银行家的口袋。<br />
更妙的还在后面，这群银行家根本连钞票都不用印，他们本来就是银行家，自己开个支票，签个名字，钱就生出来了！<br />
并且最可怕的是，美国政府永远不可能还清这笔债，因为一旦还清，那就意味着世界市场上没有了美元，那么美国人还怎么交易，事实上，只要美国的美元流通量少那么一些，就会造成严重的通货紧缩，引起巨大的金融海啸！<br />
这些本金有多少，我不知道，但我知道，每年但是利息，就至少在4000亿美元之上，个人所得税不够，就从国防支出或者其他支出里面扣，美国1999财政年度，国防部有1.1万亿美元的资金是“无法证明的调整”，接下来的一年又2.3万亿美元是无法证明的调整。<br />
这些美元，飞掉了！<br />
事实上，这些美元不是飞掉了，而是落入了华尔街银行家的口袋。 当然，作为一个中国人，我还在庆幸，我没有被这群银行家剥削，但是事实上，我们也免不了，这世界上所有的人，只要和美元有联系的，或者国家和美元有联系的，都受到了他们的剥削。我们的2万亿外汇储备，其中三分之二是美元，这群人无限制的印美元，我们的资产就无限制的贬值，从100变成50变成10再变成2变成1，事实上，经过一代人100元就变成1块钱或者5毛钱了，为什么它们不值钱了，大家都会说是通货膨胀，但钱的总的购买力是不变的，你的变少了，只是因为别人的变多了而已，这些多了的就跑到那群银行家那里了。外汇储备是哪里来的，还不是国家用我们人民的血汗钱换的？如果贬的多了又急着用，那就只能向我们收更多的税了。<br />
所以我们说中国的政府不好，管理效率底下，缺乏公平，这些都需要改变。但有一点千万不能改，就是中央银行（现在就是中国人民银行）必须是国有的，千万不能是私人的，是私人的那就麻烦大了。 再返回美国，我一直在疑惑，就是以美国政治精英们的精明，难道不会察觉华尔街的阴谋？没有试图推翻这个法律？但事实上，很少有人提起这件事情，在宋鸿兵的《货币战争》（本文如有引用这本书的内容，都会专门支出，原因就像上面说过的，这本书的很多内容无法考证）里说过，在这部法律出台之前，美国历届总统和国际银行家们进行了一百多年的激烈交锋，两次废立私立中央银行，有七位总统被刺。<br />
这些内容究竟是否是真的我不知道，但我只知道美国肯尼迪总统（就是前文所说的爱德华肯尼迪议员的哥哥，排行老二，爱德华是老四）死的时候，凶手死的很蹊跷，公布结果很不详细。调查结果被肯尼迪家族秘而不宣，说是怕引起报复。想一想就不寒而栗，在美国近一百年来，肯尼迪家族是和布什家族并列的两大政治家族，其政治能量之大，罕有其匹，父亲是著名的银行家，财大气粗，老大是二战英雄（失踪或者说战死），老二是排行前五的美国总统，老三是财政部长（竞选总统被刺），老四是最资深的参议员（因某个丑闻而失去竞选总统的机会）影响国会半个世界之久，却不敢公布总统被刺的调查结果，可见在美国，即使是总统，也不是最有权力的，我隐隐的感觉到背后有个什么组织，就像越狱里面的将军，不，比那个组织更强大，更有致命性，它就像一个不断进化的病毒。如果说一个狮子你还有办法打倒它的话，对于一个不断进化的病毒却往往无能为力，它系统的腐蚀文明。 说了这么多，无非想表达一个观点，即美国总统和国会管不了美联储，更管不了他们背后的银行家。双发只能互相利用，但政府和国会因为债务人的劣势越来越处于下风。 事实上，美国国会议员们很可能连敌人都是谁也不知道。美联储的构架，如果不说它的作用和目的的话，简直是个天才的设计。放在前台的美联储主席和董事由总统任命，但推荐名单却由咨询委员会来任命，咨询委员会有十二个地区分会，但真正控制局势的只是一家美联储纽约银行，而这家银行的董事是谁，股权配置，由谁控制，却是一个最大的秘密。 <br />
“许多年来，究竟谁拥有美联储一直是一个讳莫如深的话题，美联储自己总是含糊其辞。和英格兰银行一样，美联储对股东情况严守秘密。众议员赖特 帕里曼担任众议院银行和货币委员会主席长达40年，在其中20年里，他不断地提案要求废除美联储，他也一直在试图发现究竟谁拥有美联储。<br />
当然，这个秘密最终还是被发现了，《美联储的秘密》一书的作者尤斯塔斯经过近半个世纪的研究，终于得到了12个美联储银行最初的企业营业执照，上面清楚地记录了每个联储银行的股份构成。”（《货币战争》） 那个被蒙骗签署《美联储法》与爱因斯坦同年出生的威尔逊总统，在“去世之前，他承认自己在美联储的问题上被‘欺骗’了，他内疚的表示，‘我无意中摧毁了我的国家’”（《货币战争》） 再次引用威尔逊总统的话：<br />
“一个伟大的工业国家被信用系统牢牢地控制着，这个信用系统高度地集中。这个国家的发展和我们所有（经济）活动完全掌握在少数人手中。我们已经陷入最糟糕的统治之下，一个世界上最完全、最彻底的控制。政府不再有自由的意见，不再拥有司法定罪权，不再是哪个多数选民选择的政府，而是极少数拥有支配欲的人的意见和强波之下（运作）的政府。<br />
这个国家的很多工商业人士都畏惧着某种东西。他们知道这种看不见的权力是如此的有组织、如此的悄然无形，如此的无孔不入，如此的互锁在一起，如此的彻底和全面，以至于他们不敢公开谴责这种权力。” 然后再说911之后美国出台的新法律<br />
美国政府大规模的窃听了几乎所有本国国民的电话和网络。摘抄电影的原话，来供大家分享：<br />
President Bush has signed executifve oreders giving him sole authority to impose martial law, and suspend habeas corpus. This gives him dictatorial power over the people, without any checks and balances.<br />
布什总统签署了行政命令，给予他单独实行戒严、暂停人身保护令命令的权力。这给了他对人民实行独裁的权力，而没有人设权力制衡。 The government can jail you for life without charges,without a trial, and without a lawyer.<br />
