													<h2>马克思主义建立在六大逻辑错误基础之上</h2>
						
						
																											<div id="postmessage_825993" class="t_msgfont">荆楚<br />
<br />
逻辑学上有一条重要规则：如果大前提错了，哪怕小前提万分正确，推导过程也十分严密，那么所得出的结论，是毫无意义的。今天我所谈的“马克思主义建立在六大逻辑错误基础之上”，就是揭示马克思主义的一系列大前提错误问题。<br />
<br />
在“中国特色”的思想钳制下，专政当局不容许人民对马克思主义有丝毫怀疑。说什么“马克思主义是放之四海而皆准的普遍真理”。<br />
<br />
我从来不相信用枪杆子逼着人民相信的东西，是什么普遍真理。而是恰恰相反。所以我认为，靠枪杆子逼着人民相信东西，只能说明它的荒谬绝伦，只能说明它的心虚理亏，只能说明它在逻辑上站不住脚。因为真理是强大的。就像“两点之间的距离最短”这个定理，无论你怎样质疑和论证，只能说明它的颠扑不破。<br />
<br />
我曾经在skype语音房间上，多次给大家公开演讲过《马克思主义建立在六大逻辑错误基础之上》这个论题。但多次公开讲演之后，仍时不时冒出没有听到的人，再三要求我反复演讲。使我在时间和精力上，都感到力不从心。朋友们遂鼓励我把演讲内容整理成文，以便于人们阅读和思考。并说这样的论文，很有现实意义。还说现有的博士论文，都达不到这样的高度云云。<br />
<br />
但考虑到当局对我“刑事拘留”之后，以“取保候审”的名义释放出来，乃再三要我“封笔”。我虽然嗤笑他们“一个养着四百万党卫军的政权，却害怕一个文弱书生的一支秃笔？这样虚弱不堪的政权，还有什么存在价值？还值得你们去维护么？”但考虑到在我拘押期间，家人的担惊受怕和再三劝说，我当时被迫写下了一份“保证书”。即“保证”在“取保候审”期间，不再发表抨击时弊的文章，不再抨击党国领导人，以免触犯中国刑法上荒唐的105条……但我也预留了一个“尾巴”――即更愿意就历史、哲学、经济理论方面进行分析探讨……今天，我写《马克思主义建立在六大逻辑错误基础之上》的论题，也没有违背当初的承诺。<br />
<br />
在展开论述之前，我要事先声明：我的这篇短文，是写给普罗大众看的。因而尽可能的避免学究气。让没有多少学术功底的人，一看就明白其中的道理。所以就省略了引文的出处和注释。这对于有一定的马克思主义修养的方家，自会体会我的引言之出处。而对于普罗大众来说，繁复的引言和注释等，会让其望而却步。也因为很多时候，人们被高深玄奥的理论引入歧途，乃需要用回归常识，来进行正本清源。我的这一苦衷，特请方家谅之！<br />
闲话少说，言归正传。<br />
<br />
一、马克思主义的第一大逻辑错误是――以阶级性否认普遍人性的存在<br />
<br />
马克思说什么“在阶级社会里，人处于什么样的阶级地位，就有什么样的观点、思想和阶级立场”云云。这就是以“阶级性”来否定“普遍人性”的存在。马克思以在以“阶级性”而否认“普遍人性”存在的基础上，再推导出“阶级斗争”无处不在，无时不有。<br />
<br />
而在这个“无处不在、无时不有的阶级斗争”的基础上，又将“阶级斗争是个筐，什么东西都往里面装”作为其立论的基础。<br />
<br />
难道人类只有“阶级性”而无“普遍人性”吗？否！<br />
<br />
人们看到美丽的花朵，就会精神愉悦。闻到肮脏腥臭，就会感到不舒服。人都有亲亲之心，恻隐之心，同情之心，向善之心。人还有正义感，有良知，有道德观念。人都有共同的冷暖饥饱之感受。饥思食、渴思饮。人们都乐生畏死，趋利避害。乐安逸畏凶险，喜健康恶疾病等等。这一切，都是普遍人性的基本内涵。不因他的阶级、出身、政治和经济地位而有所不同。因此，普遍人性是客观存在的。<br />
<br />
正因为人类存在着普遍人性，人们才有共同的语言，才可能形成基本的价值观。<br />
<br />
在共同的语言和基本价值观的基础上，人们才可能相互沟通。才能够通过交流、谈判、妥协，而取得谅解和共识。才可能够达成互惠互利和共存共荣。才有谈判、妥协、斡旋的可能性。<br />
<br />
设若人类只有阶级性而无普遍人性，那么人类就无法进行任何的交流和沟通，也就不可能取得任何谅解和共识，也就没有任何谈判、妥协、斡旋的可能性。<br />
<br />
假如马克思关于“阶级性”的论断成立，那么“恩格斯现象”本身，就是对马克思这一论断的坚决否定。<br />
<br />
众所周知，恩格斯出身于英国资本家家庭，也可以说是英国上层的绅士阶层。按照马克思的阶级性的论断，那么恩格斯就必然具有资产阶级的思想、观点和立场。那么恩格斯为“无产阶级的解放事业”奉献终生，并无私资助马克思钻研和写作《资本论》，就变得不可思议了。<br />
<br />
难道人与人之间只有斗争不休吗？否！<br />
<br />
先贤孟德斯鸠在《论法的精神》中总结到：和平是人类自然法则的第一条；觅食是自然法则的第二条；相互之间的自然需求和爱慕，是自然法的第三条……<br />
<br />
马克思把阶级斗争夸大到无处不在、无时不有的荒唐程度，而否定人类的一切自然法则，实在是十分荒谬的。<br />
<br />
更为严重的是，中共篡政窃国之后，就用了郭沫若、翦伯赞等犬儒，就按照“人类一切历史都是阶级斗争的历史”的谬说来改写了全人类的历史和文明积累史。而对史实学派的众多历史学者，进行了无情的清算整肃。使那些史实学派的历史学者，不是赶下课堂，就是施以缧绁……直到今天，中国大陆的大、中、小学的教材，都建立在这个“人类一切历史都是阶级斗争的历史”为经纬的理论框架之下，继续扭曲着一代代孩子的心灵和智慧。<br />
<br />
因此，马克思将“阶级性”和“阶级斗争”扩大到无处不在、无时不有的程度，并在这个基础上所得出的一切论断，是十分荒谬可笑的。因而建立在“阶级性”和“阶级斗争”基础之上的一切推论，必然是错上加错，越走越远。<br />
<br />
二、马克思主义第二大逻辑错误是其提倡的“无产阶级专政”<br />
<br />
先从字面上来理解，无产阶级是一个人数众多的集合体概念。而专政是指由少数人把持政权，而不容其他人染指。如果由人数众多的集合体执政，就不能说成是专政。而怎样让人数众多的无产阶级来施行“专政”，马克思从来没有任何实现程序和技术手段的论述。<br />
<br />
因此，在专政问题上，后来的一切马克思主义者的脑袋里，几乎全都是一团浆糊。以己昏昏，岂能使人昭昭？<br />
<br />
撇开无产阶级专政这个概念本身的荒谬悖乱不说，马克思主义的“无产阶级专政”学说，是逆历史潮流而动的反动学说。<br />
<br />
早在17世纪，阿克顿勋爵和孟德斯鸠等先贤，就发现了权力与腐败的关系式。美利坚合众国获得独立后，在孟德斯鸠等先贤的理论指导下，进行了权力制衡的政治制度的设计和实践，并取得了很大的成功。而马克思到了19世纪，仍然倡言专政，仍然反对对权力进行制衡和约束。<br />
<br />
最近，乔治&#8226;W&#8226;布什说得好――人类文明的最大成就，不体现在科技的发达上，不体现在大师们的煌煌巨著上，也不体现在物质产品的丰富上，而体现在实现了对统治者的驯服。即把统治者关进铁笼子里，以免堕落为奴役人民的暴政……我就是被美国人民关在铁笼子里面的人……（大意）。由此可见乔治&#8226;W&#8226;布什的坦荡襟怀，并让我由衷钦佩。<br />
<br />
从人类政治文明和政治智慧的实践来看，从共产奴役制度的历史来考察，专政必然导致个人独裁，个人独裁必然导致少数人对大多数人民的奴役和暴政。毫无例外，马克思倡言的无产阶级专政学说，也必然导致独裁和暴政。因此，马克思建立在专政基础之上的一切推论，是极其荒谬的，后果也是十分严重的。<br />
<br />
有人总结二十世纪对人类文明带来空前灾难的四大学说是：一是希特勒的国家社会主义（纳粹），二是墨索里尼的法西斯主义，三是日本的军国主义，四是马克思主义。而马克思主义给人类文明带来的血泪和苦难，尤为巨大和持久。应排在首位。<br />
<br />
三、马克思主义第三大逻辑错误是仇视“公民个人财产制度”<br />
<br />
马克思把公民个人财产制度命名为“私有制”，并极尽侮骂、诋毁、否定之能事。<br />
<br />
首先，从“私有制”和“公有制”这对词汇的词性来看，就是对人们的有意误导，是一个价值判断的逻辑陷阱。<br />
<br />
因为“私”字使人们产生“私心”、“自私自利”的联想。而“公”字则使人们产生“公益”、“公正”“大公无私”的引申。因此，马克思主义者流用“私有制”和“公有制”这组极富主观感情色彩的词汇概念，来论述社会经济现象，这本身就是对“公民个人财产所有制”的污蔑和误导。<br />
<br />
其次，公民个人财产制度的确立，是人类文明的巨大进步。对他人劳动成果或劳动积累的尊重，是人类社会和谐相处的伦理底线。如果不懂得尊重他人的劳动成果或劳动积累，就使人类社会堕落成弱肉强食的丛林世界，只能遵从丛林法则。<br />
<br />
再次，公民个人财产是保障公民自由和尊严的物质基础。一旦公民个人拥有的财产变成一种社会意识的罪恶，一旦公民没有任何个人财产的保障，那么公民只能沦为掌控社会物质财富分配的官僚的奴隶，而没有任何自由和尊严可言。<br />
<br />
马克思倡言变“私有制”为“公有制”，却对于怎样变“私有制”为“公有制”的实现程序和技术方法没有任何论述。也不可能有任何实现程序和技术方法的论述。<br />
<br />
马克思无法解决的这一理论和实践的空白，这就为后继的马克思主义者的阴谋家们创造了随意解释和操作的空间。他们的“技术手段”只能是践踏人权、蔑视人性和人道的非法掠夺。<br />
<br />
苏俄和中国的马克思者阴谋家们，正是在“公有制”的随意性和不确定性的基础上，用特权、暴力和非法掠夺等“技术手段”，创建了一种挂着“公有制”的狗头、而实际上是“官僚特权所有制”的极端腐朽的政治制度。<br />
<br />
在这种“官僚特权所有制”的基础上，对于克里姆林宫和中南海的阴谋家来说，他们真正实现了“各尽所能、各取所需”的共产主义的理想――他们想要什么就有什么。包括全国各地的美女。只要他们的一个电话，或使一个眼色，各级权力机构的奴才们，自会屁颠屁颠地源源不断地送进去，供他们淫乐和享用。只是他们把苏俄和中国拖回到奴隶社会中去了而已。<br />
<br />
喧嚣一个多世纪的共产主义运动，无一不是而将“公有制”演变成官僚特权所有制。而这样的官僚特权所有制，无一不是世界上最严酷、最反动的奴役制度。无一不是以对社会生产力和人类文明的极大破坏而告终。<br />
<br />
如果说喧嚣一个多世纪的共产主义运动对人类文明有所贡献的话，这个“贡献”就是做了一本很好的反面教材。这本反面教材是用人类的累累白骨、血流成河而写成的。<br />
<br />
四、马克思主义第四大逻辑错误是蔑视人性、人道、人权的普世价值<br />
<br />
“共产党人不屑于隐瞒自己的观点和意图。他们公开宣布：他们的目的只有用暴力推翻全部现存的社会制度才能达到。”<br />
<br />
为了达到暴力夺取政权之目的，就要不择一切手段。为了达到其非法剥夺公民个人财产而建立“官僚特权所有制”的社会制度之目的，马克思和后继的马克思主义者流，必然要践踏一切人类文明的价值观和伦理底线。因为“人道”、“人性”、“人权”等普世价值观，必然成为他们“不择手段”的理论枷锁和思想滞绊。<br />
<br />
当人性、人道、人权等普世价值成为马克思主义者流夺权、夺产、夺命之理论枷锁和思想滞绊的时候，他们便毫不犹豫地飞起一脚，将这些人类文明积淀的瑰宝踢进了臭水沟。<br />
<br />
我始终坚信，一种学说，无论它的理论体系是如何庞大，也不管他的论证是如何严密，如果是以人性、人道、人权等普世价值为敌，那么我只能说它是歪理邪说，是反人类、反社会、反文明的是邪教。<br />
<br />
由于马克思主义否定普遍人性，漠视人性，践踏人权，并与人性、人道、人权为敌。恨不得消除殆尽而后快。把人类文明积累的瑰宝极不负责地冠上一个“资产阶级的”帽子之后，便一脚踢进了臭水沟，说人性、人道、人权等普世价值是资产阶级的遮羞布云云……<br />
<br />
一个迷失了人性、人道、人权价值的社会，只能是一个两脚动物的丛林。而按照马克思学说创建的政治制度，必然是一个“率兽食人”的奴役暴政。<br />
<br />
由于马克思主义仇视人类文明的基本价值观和伦理底线的精神指归，我只能说马克思主义是一种反人类、反社会、反文明的歪理邪说。而建立在这种歪理邪说基础上的一切推论和推理，毫无疑问，是十分荒谬的错上加错。<br />
<br />
五、马克思第五大逻辑错误――是以对某些牧师或神父的虚伪的揭露，来代替对有神论的否定<br />
<br />
马克思认为，人仅仅是一个物质存在，而否认人的精神和灵魂存在。把人说成只是物质的人，说成是动物性的人，否定人的精神伟大和灵魂高贵。如恩格斯说：“生命不过是蛋白质的存在方式”。为了实现某种见不得人的目的，无缘无故地把人杀掉，只不过是改变一下“蛋白质的存在方式”而已。将这句话往深处想想，能不让人毛骨悚然？<br />
<br />
人仅仅是一个物质存在么？否！<br />
<br />
其实，人一方面是一个物质的存在，另一方面且更是一个精神和灵魂存在。人如果仅仅是物质的存在，岂不是“行尸走肉”？<br />
<br />
马克思和恩格斯单方面强调人的物质存在，而否认人的精神和灵魂存在。明眼一看，就知道他们有多么荒谬。<br />
从人类过往历史来考察，恰恰是那些人格高尚、精神伟大和灵魂高贵的人们，才是推动人类文明进步的主要力量。<br />
<br />
马克思为了达到否定人的精神伟大和灵魂高贵之目的，就极力否定有神论和唯心主义的一切学说，而片面坚持无神论和唯物主义，片面坚持人的动物性的一面，而否定人作为精神和灵魂的存在。<br />
<br />
曾经有朋友开玩笑说，“按照马克思的唯物主义学说，既然人的精神和灵魂的存在是子虚乌有的，那么人类的爱情也是根本不存在的。”这虽然是句玩笑话，却颇能揭示马克思主义的荒谬所在。<br />
<br />
而马克思否定有神论的技术方法，就是使用了逻辑学上的偷梁换柱之术。即以对某些神父和牧师的虚伪的揭露，来代替对有神论的否定。<br />
<br />
我们知道，意大利的薄迦丘对某些神父和牧师的虚伪的揭露，其笔锋可以说是入木三分、淋漓尽致，比马克思之流的揭露要深刻得多。但薄迦丘的根本目的，是为了维护信仰的纯洁和神圣，而不是为了否定其信仰。<br />
<br />
马克思在这个问题上，实在是闹了一个低能弱智的大笑话。<br />
<br />
而建立在这样一个弱智低能的大笑话基础上的一切推论，只能让后人笑掉大牙。<br />
<br />
六、马克思主义以社会达尔文主义为哲学根基<br />
<br />
追本溯源，马克思主义的哲学根基，是建立在社会达尔文主义的基石之上。<br />
<br />
世界上有几大学说是以社会达尔文主义为哲学根基。其一是马克思主义，其二是德国的国家社会主义（又译成“纳粹主义”），其三是意大利的法西斯主义，其四是日本的军国主义。所有这些主义和主张，都给世界和平和人类文明带来了空前的血泪和灾难。<br />
<br />
达尔文学说在现代基因科学、遗传科学和地质科学面前，已经是错谬百出，站不住脚。人们只能将其理解成一种假说。更何况“社会达尔文主义”！<br />
<br />
可悲的是，在中国的思想文化环境里，由于受到中共几十年的单向性意识形态导向，再加上几十年用唯物主义、无神论的意识形态来控制人们的思想，并用枪杆子为后盾进行强制灌输。在中国，能知道达尔文学说荒谬之处的人们，实在是太少太少了。能知道社会达尔文主义对人类文明巨大危害的人们，那就更加稀少。<br />
<br />
按照毛泽东的反复声言的―― “彻底的唯物主义者，是无所畏惧的。”因而他们做起坏事来，就没有任何道德良心上的压力，没有任何精神上的负担。这才造成了这些“彻底的唯物主义者”好话说尽、坏事做绝的现实，才造成了中南海是魔鬼政治、阴谋政治的别称。<br />
这不仅是中国人民的不幸，也是人类文明的灾难。<br />
<br />
因此，以社会达尔文学说为哲学根基的马克思主义，就是在错误基础之上的错上加错。<br />
<br />
至于后来的列宁主义、斯大林主义、毛泽东思想等缪说，就更在马克思主义的逻辑错误的基础上愈行愈远。<br />
<br />
对于毛泽东思想的荒谬绝伦之处，我将在嗣后的《我为什么说毛泽东比不上我的一根卵毛？》一文中，继续进行分析讨论。就此刹住。<br />
<br />
写于民国97年8月28～29日<br />
