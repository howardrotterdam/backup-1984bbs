																											<div id="postmessage_826330" class="t_msgfont">第一条有点问题，“阶级性”就是“普遍人性”，这个逻辑是这样的――人首先是经济的动物“人只有先吃饱了饭，才能从事其他更高级的精神活动”，而在经济生产中产生分工，产生所有制，产生阶级。所以说“阶级性”是“普遍人性”是没错的，问题在于，它不是唯一的普遍人性。<br />
<br />
第二条基本赞同，我做点补充。<br />
<br />
巴黎公社的时候，政府是多“党派（也许说政治俱乐部更合适）”联合执政的形式，马克思也明确地说过“巴黎公社是无产阶级专政”。这种在战争形势下还倾向于社会民主主义的组织形式的“无产阶级专政”，和列宁战争形势下的的&quot;无产阶级先锋队专政&quot;是不同的，与斯大林“和平时期的无产阶级先锋队专政（为了教育“落后群众”）”更是不同。<br />
<br />
按照《共产党宣言》中的原话“它在消灭这种生产关系的同时，也就消灭了阶级对立和阶级本身的存在条件，从而消灭了它自己这个阶级的统治。”无产阶级专政只是国家机器消亡中的一个过渡阶段。<br />
<br />
放到伯恩施坦、考茨基那里，工人普选的结果就是“无产阶级专政”了。<br />
<br />
第三条要放在整个历史领域里看。<br />
<br />
马克思说“资产阶级在它的不到一百年的阶级统治中所创造的生产力，比过去一切世代创造的全部生产力还要多，还要大。”只有到了资本主义发达的时候，无产阶级革命的条件才成熟，“资产阶级用来推翻封建制度的武器，现在却对准资产阶级自己了”。<br />
<br />
因此，泛泛地说马克思在任何历史阶段都仇视私有制是不对的，在恩格斯的《家庭、私有制和国家的起源》里，从“原始的公有制社会”过渡到“私有制社会”是一种历史的进步。<br />
<br />
第四条也同意。<br />
<br />
马克思主义很大的问题就是其思想的“一元性”，既然有人掌握了“唯一的历史真理”，那么“制衡”“质疑”“批判”“自由”这些东西都是不必要的，唯一必要的东西就是“服从”，在这个意义上，马克思是自己的掘墓人。<br />
