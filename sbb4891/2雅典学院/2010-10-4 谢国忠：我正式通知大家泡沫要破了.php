													<h2>谢国忠：我正式通知大家泡沫要破了</h2>
						
						
																											<div id="postmessage_854676" class="t_msgfont">近日，独立经济学家谢国忠正式警告，中国大陆楼市泡沫要破了，熊市将持续五年。这是谢国忠继9月18日指“香港、上海等一线城市房价已经见顶”之后的又一大胆预测。 <br />
　　<br />
据《东方早报》之前报到，谢国忠于9月18日在上海银行秋季投资报告会上表示：“香港、上海等一线城市房价已经见顶！”。他称，修正此前对中国房地产市场逐步回暖复苏的判断。他甚至建议，手上有多套房产人士不应再考虑购房，相反可以从当下开始卖房。 <br />
“正式通知大家泡沫要破了” <br />
　　<br />
作为玫瑰石顾问公司董事、经济学家，谢国忠日前对彭博通讯社表示：“我现在正式通知大家，楼市已见顶，接下来将逐步下跌，熊市将持续五年，大城市的平均房价可能会跌一半以上又到了吃大闸蟹的季节。” <br />
　　<br />
他说，煮螃蟹，首先你把蟹放在冷水锅中，它们会觉得又回到了湖里，浑然不觉危险。然后，盖下沉重的锅盖，开火。这时，你可以听见它们抓挠锅壁的声音，开始轻，然后拚命挣扎，然后轻，再后就偃旗息鼓了。几分钟后，你打开锅盖，蟹壳已成金黄。这时，你就可以蘸着醋姜，享受美味了。 <br />
　　<br />
现在，中国的炒房人就像在冷水锅中的螃蟹。他们感觉很好，会时不时地伸伸腿，看不到任何危险。殊不知，自己已经被人盖上锅盖、下面点上了火，马上就要被煮熟了，却尚未发觉。 <br />
　　<br />
谢国忠说：“今年4月，我告诉过读者，楼市泡沫即将破灭时，就会通知大家。现在，楼市已经见顶。在今年余下的时间内，它将逐步下跌。到2012年，人民币升值预期可能会逆转，资本会流出，届时楼市会加速下跌。” <br />
　　<br />
谢国忠认为，中国已进入房地产熊市，将持续五年时间。大城市的平均房价很可能会下降一半以上。土地价格将下降更多。在泡沫最大最疯狂的浙江省，价格可能会下降80%甚至更多。 <br />
<br />
“准确断言市场顶部是一门艺术” <br />
　　<br />
他说，在泡沫时期，准确断言市场顶部是一门艺术。人类跟风投资的弱点是泡沫的助推剂，总到最后时刻才能醒悟。泡沫破裂通常由资本退潮触发。有时，超量供应会超过投机性需求，足以吓跑投机者。多数泡沫是突然破裂的。但也有个别泡沫是逐渐泄漏的。 <br />
　　<br />
1997年，外国资金撤出推高了利率，导致香港楼市泡沫破灭，顷刻间，吸光了这个赌场的所有资金。2000年互联网泡沫的破灭，是因为那些上市公司的大股东们手中的股票限售期刚过即大规模抛售，惊醒了投机者。而2007年美国楼市泡沫破灭，是因为次贷到期了，投机者发现还不上钱。 <br />
　　<br />
谢国忠说，1998年，中国的房地产泡沫也破灭了。三年内，上海房价下跌了三分之二。广东和海南的烂尾楼随处可见。再近点说，上海的房价从2005年5月至2006年底下跌了三分之一。很多商业区的楼盘甚至下跌了一半。 <br />
　　<br />
他认为，现在的泡沫始于2007年，由宽松的货币政策和人民币升值预期催生。“我原以为，它也会在瞬间破灭。但最近的事态发展改变了我的观点。此次的泡沫可能不会瞬间破灭，而是会慢慢泄气。之前，我以为政府可能会在四季度放松宏观调控，导致房价再次疯涨，泡沫会在2011年下半年或2012年突然破灭。” <br />
　　<br />
政府目前仍然没有放宽二、三套房的贷款限制。那些最有潜力的买房者受到了管制，这些买房者其实并没有足够的现金，主要依靠贷款买房，因此，房价不能更上一层楼了。 <br />
　　<br />
谢国忠预计，现在中国房地产价格正在慢慢下跌，并将于2012年加快步伐，届时，中国经济也将会受挫。他警告：“楼市的投机者们就像冷水锅中的大闸蟹，正在挨煮。但是，趁着现在火小、水温低，聪明人还有时间逃出升天。” <br />
　　<br />
他还说，为了防止房地产泡沫泄气而影响到其它投资领域，政府必须迅速向银行注资。政府有足够的财力和智力这么做。在过去十年，中国拥有强大的楼市和疲软的消费。而未来十年，情况可能刚好相反。 <br />
