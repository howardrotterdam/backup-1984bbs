																											<div id="postmessage_547606" class="t_msgfont">1948年共产党战胜国民党的真正原因<br />
<br />
主讲：上海交通大学历史系教授 刘统<br />
<br />
刚才来的时候有读者问我，你写的书每一本都是既没有前言也没有后记，你的书是怎么写出来的？我先简单地自我介绍一下：我原来是嗡嗡嗡时候的老三届，我是老初二，以后赶上嗡嗡嗡，成了黑五类，嗡嗡嗡中在工厂当了九年工人，当到三级工。1977年恢复高考的时候我考本科，结果我家政审还没有落实政策，没有被录取。接着1978年第一届招考研究生，我就考到山东大学历史系中国古代史专业硕士研究生，学魏晋南北朝隋唐史，我的第一位硕士导师是王仲荦先生。毕业以后我留在山东大学历史系，当时王先生主张我再开拓一下眼界，继续深造，于是我1985年考上了复旦大学历史地理研究所，跟谭其骧先生学中国古代历史地理。人一辈子如果能够碰上一位名师就是很大的幸运，而我碰到了两位名师，非常幸运。毕业的时候想回北京，当时正好中国军事科学院需要研究人员，就把我招去当兵了。<br />
<br />
到了军事科学院以后跟我坐对桌的就是一位年轻的少校茅海建，他虽然比我年轻，但他是老兵，他当兵的时候就在东海舰队，1982年硕士毕业以后就到军事科学院，而我是一个新兵。他向我介绍，军事科学院是叶帅创建的，军事科学院里的宝贝就是叶帅当年积累保存的战争档案。茅海建是中国近代史专业的，他没时间读解放军的战争档案，他建议我把这些东西好好地读一读。后来我就到图书馆借这些战争档案看，使我非常震惊，这些东西太好了。一边读着档案，一边向军事科学院的老同志请教，这些老研究员有两种成分，一种是解放战争时期四大野战军的老参谋，还有一种是建国后大将元帅的秘书，他们喜欢给我讲一些故事，那个时候我才明白原来我们党的历史有两种，一种是教育群众用的，比如高中的教材、大学的党史课等等；还有一种是真正的历史，那是藏在我们的档案里，或是当事人口口相传的。我看到这些以后，就下了决心，不再搞历史地理了，改行做现代史、军事史。<br />
<br />
我最后一次见到谭其骧先生是1990年，他中风前的半年，他到中国科学院开院士增选会议。我跟他说起我在军事科学院看到的一些东西，谭先生非常激动，当时便站起来说：“你要把这些东西都记下来！”既然导师允许了，我就自己改了专业，在军事科学院一蹲15年。这期间主要写了三本关于解放战争的书，都是在人民出版社出的，写了一本长征后期毛泽东与张国焘斗争的书《北上》，三联编辑看了这些书以后希望我再拓展一些视野，把国民党和共产党放在平等的地位上做比较，然后再挖一些深层次的东西，于是我就写成了《中国的1948年》。写了这本书以后还是觉得意犹未尽，因为有些东西也不可能放开来写，总是要在表述上比较婉转，内容上也是要有所保留。但是我还是希望，凡是在我书中提供给读者的都是真实的，真实到什么程度那要看社会发展和国家开放的情况，也许今后要不断的修订、补充。<br />
<br />
我今天讲“1948背后的故事”就是想给大家澄清一些误区，讲一些真实的历史。我们现在改革开放20多年，可以说在战争题材上也比较开放，但是不管怎么开放，给大家总是有这么一个印象：我们共产党、解放军的形象曾经塑造了两个代表性人物，一个是电视剧《激情燃烧的岁月》里的石光荣，一个是《亮剑》中的李云龙，这其实是文人写的军人，不是真正的解放军。如果解放军的将领都像石光荣和李云龙这样满口的粗话、不管三七二十一，打仗能打过国民党吗？国民党将领许多都是受过高等教育的，是留过洋的，而且人家一开会的时候都是军装笔挺、将星闪耀，咱们解放军的干部开会有的蹲在凳子上，有的叼着烟袋，什么样的都有，这么土里土气的共军怎么能把国民党打的落花流水，这里必有非常重要的原因。国民党将领的回忆录，一提起三年的解放战争，几乎没有一个服气的，像胡琏，国民党五大主力之一，18师的师长，在华东战场几次跟粟裕较量，在淮海战场上只身逃脱。最后胡琏打胜的一仗就是金门。胡琏说共产党打仗是人海战术，前面一群老百<br />
<br />
姓拿着手榴弹往阵地上冲，我们能忍心开枪吗？我很负责任的对大家说，我查遍了解放战争的档案，可以说解决战争重要战役的档案我都看了，但是没有看到一个战例解放军是这么打仗的。胡琏可能是一种误解，因为我们解放军当时装备、服装肯定不如国民党军，有很多战士是装不上正规军装的，所以胡琏的这种说法没有根据。<br />
<br />
另外，我对国民党没有那么深的成见，我还是能比较客观地看国民党的战争档案。看后我有一个感觉，蒋介石不像是我们书里描写的样子，专横跋扈、一意孤行、心胸狭隘。<br />
<br />
我看了蒋介石的全集，尤其是跟共产党决战几年中他一系列的讲话、决策，我发现他的想法大多数都是对的。早在1948年初他就预感到东北守不住，于是让卫立煌把东北的精锐部队全部撤到关内，应该说蒋介石当年的这个决策是对的――如果卫立煌把60万部队及早撤到关内来，那么解放战争要打多少年还说不定。第二个重大战略决策是，在卫立煌守不住的时候，蒋介石做傅作义的工作，让他放弃北平天津，把华北的50多万精兵撤到长江以南。结果傅作义也没有答应，如果他按照蒋介石的指示做了，那么我们的平津战役也找不到战机了，不能在黄河以北歼灭国民党军的主力，那我们国家说不定真要出现南北朝划江而治的局面。但是蒋介石两大决策都没执行，是什么原因？主要是国民党里面派系林立，各有各的盘算。卫立煌想，蒋介石一贯嫁祸于人，万一出现问题，他到时候把责任推给我我就完了。傅作义想，我本来是华北人，华北是我的天下，我到了江南我算什么？一个地方军政的长官怎能没有根基。蒋介石的种种决策都是因为下面的扯皮、推诿不得以实现，所以蒋介石才越来越着急上火。<br />
<br />
蒋介石在战略上是有预见性的。在1949年2月蒋介石已经秘密命令上海和南京的官员把上海银行里所有的黄金、白银、银元转移到台湾，而且从那时候就开始修舟山机场，准备把国民党大批的官员、难民撤到台湾。解放军还没渡江之前蒋介石已经料到这一步了，所以他能从容不迫地把南京故宫博物院的国宝运到台湾。但是蒋介石最终还是失败了，把大陆丢了。在过去我们把蒋介石的失败都说成是腐败――国民党腐败、不得人心，但是真正打仗的人、真正在战场上的人心里明白，战场上是你死我活的较量，容不得双方有半点的犹豫，更别说腐败了，所以在战场上，无论是国共双方都是真刀真枪的较量。如果要说一次两次战役的成败或许有偶然因素，但是整个战争的失败是不会偶然的，还是有许多必然的因素。这就是今天我想给各位读者解释的事情。<br />
<br />
我们正规的历史里对毛泽东同志过去达到了歌颂和迷信的程度。我们的历史上写到，抗日战争结束之后中国共产党为保卫自己的胜利果实和国民党展开了较量，是不是说抗战一胜利共产党就想跟国民党争天下？现在越来越多的历史披露出来，尤其是《胡乔木回忆毛泽东》这本书出版以后，还有《毛泽东传》的出版，让这段历史基本上清楚了，没有疑问了。在抗战刚结束的时候中国共产党并没有打算跟蒋介石争天下，而是希望在国民政府里拥有一席之地，因为抗战刚结束的时候蒋介石的个人威望达到了顶点，他是中国抗战的领袖。而且根据共产党当时的实力，虽然有了一亿人口的根据地、90万人的军队，但是相比国民党还是差得远，所以毛泽东赴重庆谈判就是希望跟蒋介石谈出一个和平来。当时双十谈判协定规定给共产党提供几条，一个是华北五省的主席由共产党担任，另外全国军队缩编成100师，共产党可以占20师的配额。毛泽东对这个结果是相当满意的，回到延安跟胡乔木和中央负责人说，蒋介石并不反共。当时中共中央确实想和国民党搞联合政府，毛泽东曾一度想把首府从延安迁到淮阴，淮阴离南京比较近，去开会很方便。但是蒋介石积20年的反共经验，是绝对容不下共产党的，所以当双十协定要实施的时候国民党不断向共产党施加压力，并且派遣大军从大后方源源不断地向华北、东北扩张。<br />
<br />
中共当时要跟蒋介石争夺内地是不容易的，但当时东北是一块空白，国共双方在那儿都没有基础，于是共产党就占着地利之先，十万大军挺进东北。这一下蒋介石非常着急，因为东北是中国的工业基地，蒋介石必然要占这块地方，所以蒋介石派了缅甸远征军最精锐的部队在杜聿明的指挥下开赴东北。解放军十万大军闯东北，林彪指挥的部队可以说是五湖四海，哪个根据地的人都有，毛泽东指示林彪在四平跟蒋介石打一仗，要是把国民党打赢了你就得承认我在东北的合法性，要是打输了再说。国民党对四平这一战也是非常重视的。所以1946年5月的四平保卫战也是林彪和杜聿明之间真刀真枪的第一战。我们的东北民主联军当时是各地的部队集合起来的，林彪当时到了那儿以后这个也不认识那个也不熟悉，可以说是仓促打了这一场正规战。国民党进东北的口号是“接收东北主权”，所以这些军队进去以后士气也是相当高，四平这一战林彪因为武器不行、兵源不行、指挥也不<br />
<br />
协调，守了一个月最后败了，林彪当时是最难受的一段时间，兵败如山倒，退过松花江，就剩下哈尔滨、北满那一块地方。蒋介石四平这一仗打赢之后，下定决心非要消灭共产党不可了。<br />
<br />
毛泽东跟国民党打还是不打，胡乔木说毛泽东苦苦思索了三天三夜，最后决定跟国民党彻底决裂，打。但是打的赢打不赢，当时共产党的将领中几乎没有几个人认为在当时的条件下能打赢国民党。当时最有代表性的就是李富春、黄克诚给中央写了一封长篇报告，说二次大战以后苏联受了很大的伤害，不能马上支援我们，国民党现在得到美国的支持又是那么强大，我们现在经过四平这一仗，要认识到以目前的实力不具备与国民党对抗的条件，所以我们现在要忍让、积蓄力量等待国际时机成熟的时候再起来反攻。这份报告可以说是代表了当时中共党内高级将领的普遍思想。<br />
<br />
毛泽东写了一个答复，他说报告说的都是真实情况，但是有一个缺点是对蒋介石的困难估计不足，二次大战以后国际进步力量不是下降了，而是上升了，我们如果坚决斗争，可能比退让要好得多；如果没有斗争精神，结果将极坏。毛泽东这个指示发表在毛泽东的军事文集和毛泽东文集第四卷上。我当时看了以后觉得这是一个很奇特的结论，蒋介石比共产党还困难，谁相信呢？结果事实证明蒋介石就是比毛泽东困难，毛泽东当时就是要占地盘、扩大根据地、扩大军队，但是蒋介石作为国民政府的领袖有好多事情要做。<br />
<br />
第一是恢复沦陷区，伪军日本占了那么大的地区他要接收，要把一个一个城市的政权恢复起来，还要养活沦陷区的老百姓，还要处理伪军和日本的战犯，还要把日本的几百万军人和家属遣返回日本，国民政府要干的事情多了。所以毛泽东这个论断就是说不要光看到我们自己有困难，你要看到蒋介石比我们更困难，这就显示了毛泽东作为一个战略家的远见。解放战争在1946年7月正式揭开战幕，三年打下来，居然是国民党一败涂地。这个过程相当的复杂，但是我们说在战争表象的背后必定有一些条件，共产党能够做到的但是国民党做不到。这些条件是怎么转化的，为什么越来越有利于共产党，越来越不利于国民党呢？我看了解放军的战史和解放战争的历史，我觉得过去的结论都过于标语口号化，像国民党代表反动，我们代表正义；我们得人心，国民党不得人心；我们解放区是阳光灿烂，国民党是一片黑暗。我觉得这里面需要好好地研究。我从个人的研究中悟出来几条，共产党能够得天下、国民党最终走向失败的原因。<br />
<br />
国民党之所以失败、共产党之所以胜利，第一条就是共产党解放军能够集中优势兵力，敢于大踏步的前进和大踏步的后退，不在乎一城一地的得失，而国民党处处受到牵制，有优势，但是无法集中兵力。为什么这么说？国民党每收复一个地方就有守土之责，比如占领沈阳、长春要留下一个军，占领张家口、鞍山得留下一个师，占领一个县城起码得留下一个连，国民党越前进、占的地方越多，八百万兵力就越分散，把部队都分散开了，越前进则可以集中的兵力、可以用于前线作战的兵力越少。共产党则不同，毛泽东的原则就是集中优势兵力，所以丢多少地方他不在乎。像解放战争刚开始的头两个月，共产党丢了106个城市，像样的城市都丢光了，像延安、临沂、张家口、四平等等，大踏步地后退，但是共产党丢了那么多地方兵力损失了多少呢？没损失多少。他走了以后，老百姓可以就地掩蔽，该怎么样怎么样，反正国民党来了以后，他是政府，他也不能大屠杀，共产党可以把自己的兵力调来调去，在不利的时候就大踏步地后退，甚至后退到什么程度呢？这里可以举个例子。四平之战之后，杜聿明大举进攻，林彪带着军队全线退过松花江，国民党的军队到了松花江边上突然停止前进了，当时林彪做了最坏的打算，哈尔滨准备放弃了，他带着主力部队到北满的树林里打游击。但是杜聿明到了松花江边上不动了，蒋介石也很着急，为什么不前进？不是不想打，而是兵力不足。只有一个军过了松花江，那么大的地盘怎么打？所以他被迫停止了前进的步伐。而南满当时是肖华指挥的部队，国民党进攻的是刘玉章的52军，共产党的军队节节后退，一直退到安东，国民党军队眼睁睁地看着共产党的部队从鸭绿江大桥上跑到北朝鲜去了。国民党军队过不去啊，他是政府军，要出国得有外交手续。大家看三下江南、四保临江，最后南满就剩临江县一个据点了，南满部队后方伤病员全都在北朝鲜，当时鸭绿江冻住了，所以共产党的军队在北朝鲜境内往来自如，养好了伤、准备好武器再打过来。看着共产党是处于绝对的劣势，但是共产党这个办法国民党学的来吗？所以共产党在战争初期表现的相当的灵活，就是不在乎一城一地的得失，能往哪儿跑就往哪儿跑，但是国民党的行动就受到种种牵制。<br />
<br />
过了一年，共产党能够反攻了，蒋介石集中起兵力来跟共产党决战还是不行，共产党想打哪儿打哪儿，蒋介石又处于被动状态，因为他的部队在全国散开了，哪个城市都要守，共产党就抓住蒋介石全盘散开的机会集中兵力一个城市一个城市打。比如1948年3月粟裕打开封，本来粟裕是想在陇海线跟国民党进行决战，但是一看国民党摆开大军不好打，怎么办？找防守薄弱的地方打，开封当时是河南省省会，只有一个师的国民党军队保卫，粟裕避实就虚打开封，一个师怎么能挡得住华东野战军三个纵队（相当于“军”），一下就打下来了。当时正好南京在开国民大会选总统，一听说开封被打下来了，河南的代表们连夜跑到总统府，有的下跪有的痛哭，非让蒋介石把开封收回来不可，蒋介石没办法，下令五大主力之一邱清泉的5军把开封收回来。邱清泉本来在商丘摆好阵势等着粟裕，明明知道开封是一个空城，毫无价值，但是他也得执行命令把开封收了回来。然后国民党登报纸“国军收复开封”，其实粟裕绕到邱清泉的背后，又把区寿年兵团一口吃掉。与此同时陈赓打洛阳，洛阳守军也是一个师，而且这个师是学生兵，更守不住陈赓，陈赓把洛阳打开了，国民党赶紧叫18军从驻马店向洛阳增援，结果胡琏到了洛水边上，眼看着洛水涨水过不去，等胡琏过了洛水共产党又走了。国民党总是集中不起兵力、总是被动分散。1948年以后蒋介石终于醒过来了，组成重兵集团准备跟共产党进行决战，但是为时已晚，因为当时共产党的优势已经形成了。毛泽东在十大军事原则里把集中优势兵力列在第一条，蒋介石懂不懂集中优势兵力？当然懂，但是他作为一个国民政府受到太多条件的制约，他是心有余力不足。<br />
<br />
共产党打天下的第二个绝招就是善于化敌为我，说白了就是改造俘虏兵，这是陈毅先发明的。1947年5月孟良崮战役，消灭了国民党五大主力之一的整编74师，说是歼灭3.2万人，实际上打死的不到一万，俘虏74师8000多人，当时把74师的俘虏集合起来分俘虏，结果华东野战军的各个纵队都来抢74师的俘虏，因为74师士兵素质特别好，是模范师，受过非常正规的训练，每个士兵起码都有高小文化水平，解放军里的连长都不一定有高小的文化水平。74师8000多俘虏被分到华东野战军的各个纵队里，陈毅说这些俘虏兵真顶用。1947年底陈毅从山东解放区到陕北见毛泽东，沿途路过晋绥军区，当时晋绥区的同志说华东野战军打仗打的好，请陈毅做报告，陈毅总结了一条经验，我们华东野战军为什么迅速壮大？就是会用俘虏兵。俘虏兵是好东西，战术素养非常好。我们要是招翻身农民当兵，得训练他打枪、扔手榴弹，然后才能打仗，俘虏兵来了就能用，而且在战争里表现相当不错。有一次打敌人一个据点，连长冲着机枪手说“打打打”，机枪手是一个俘虏兵，说“你让我往哪儿打”？连长说不知道。俘虏兵说怎么怎么打，连长说好，就这么打。陈毅讲，“你看，俘虏兵就是比我们的连长水平还高”。解放军过去缴获国民党的炮，当时主要是山炮，打仗时把山炮和步兵一块儿推到阵前，推到离前沿还有二百米、一百米的地方，人从炮筒里往前看目标，叫“直瞄”。被俘虏的国民党炮兵看着解放军的炮兵笑，说你们这是拿大炮拼刺刀。他们把炮拉到3000米外，算好射程，没有射不准的。这样的经验一推开，中央就下文件，以后各个部队凡是抓到国民党俘虏，除了重伤还有患传染病的，其他的一个不许放，统统补充到解放军的部队里。<br />
<br />
我们可以算笔账，国民党、共产党一边5个人，把国民党军打了一个，就是5:4，可是如果把国民党的俘虏抓过来自己用了，就变成6:4。共产党这么多兵是从哪儿来的？大部分都是俘虏兵。国民党的俘虏兵凭什么就转过头为共产党卖命？共产党有本事。俘虏兵被俘虏之后先开会诉苦，进行阶级教育，国民党兵大多数也是穷人，于是老战士先上来诉苦，地主怎么压迫我，我们打天下为穷人翻身谋解放，启发俘虏兵的阶级觉悟；然后把俘虏兵下放到各个班，但是有一条，肯定这个班里老战士占多数，俘虏兵新战士占少数，班里不能歧视虐待俘虏兵，俘虏兵被叫做“解放战士”。行军的时候班长替他们扛枪，宿营的时候班长给烧洗脚水，从人的情感这方面来说特别容易感动俘虏兵。国民党官大一级压死人，但是共产党的官不像官，班长、连长都来关怀战士，从感情上来说俘虏兵也容易被改造过来。<br />
<br />
在立功方面，俘虏兵和解放军的老战士一视同仁，这样就大大激发了俘虏兵的积极性，打好了一样立功。当时刘伯承培养了一个战士王克勤，原来是国民党的一个机枪手，俘虏过来以后补充到部队里，很快立功升了班长，他特别会带兵，他带的兵不但会打仗，而且在作战中伤亡很少，刘伯承看中了这个人，表彰为二野的战斗英雄，后来王克勤在战争中牺牲了，刘伯承还特别的悲痛，王克勤就是国民党俘虏兵里一个优秀的代表。<br />
<br />
到了1948年初，共产党熔化改造俘虏兵到什么程度？华野当时总结了经验，叫“即俘、即补、即战”，也就是上午俘虏，中午补充到解放军的部队里，下午就参加作战。国民党怎么也比不过共产党，国民党抓来壮丁以后整训，然后整编，然后上战场作战，这个周期最少大半年，刚打一仗就给共产党送了礼了。这个仗国民党越算越赔。74师是1947年5月在孟良崮被华东野战军歼灭，蒋介石非常伤心，保留74师的番号，在江南重新组建，最后组建成74军， 1948年10月74军拉上淮海战场，到1949年1月陈官庄之战74军再次被共产党消灭了。蒋介石辛辛苦苦训了一年，好不容易上战场了，几天就报销了。共产党用这种方式不断地补充自己的兵源，在一年多之内就跟国民党的兵力达到了相当的程度。所以毛泽东说我军人力、物资的来源主要在前线，就是靠俘虏国民党的俘虏兵、缴获国民党的枪炮来壮大解放军。<br />
<br />
当时战争打的相当残酷，淮海战役第一阶段，粟裕的主力跟黄百韬兵团在碾庄打，这是淮海战役第一阶段，打了将近有一个月，结果华野歼灭了黄百韬兵团7万多人，但是也付出了伤亡5万多人的代价。当时华野有一个连报了220人伤亡，一个连满编才120人，由于不断冲锋不断补充兵源，所以伤亡竟超过了满编的人数。粟裕在1949年3月给中共中央写淮海战役的总结，说1948年10月济南战役的俘虏兵到1949年1月打陈官庄时有的已经提升为副排长了。共产党这么大的伤亡，都是靠俘虏兵一拨一拨更换，国民党是打一个少一个，有出没进。这样的话，共产党和国民党的兵力对比变化能不快吗？<br />
<br />
大家可能要提问题了，共产党能改造俘虏，国民党怎么不行呢？孟良崮战役之后蒋介石召开高级将领的军事研讨会，他沉痛检讨74师失败的原因，说74师之所以失败最大的失误就是用了共产党的俘虏兵。涟水战役中，74师把涟水打下来，当时守涟水的是华野六纵。六纵被张灵甫俘虏了300多人，张灵甫让共军的俘虏兵当辎重队，拉大炮、拉粮食，蒋介石说绝对不可以这么做，绝对不可以信任共军的俘虏兵，张灵甫说不要紧，不是让他们当战斗部队，他不当回事。孟良崮战役之前，国民党军10个师一线推开向北走，张灵甫自己带的一个师突出了两天的路程，他把主力部队驻在孟良崮，重炮部队安排在山下的垛庄，陈毅、粟裕抓住张灵甫突前两天的战机，一下用六个纵队15万人包围了张灵甫的3万人，扑垛庄的正好是王必成的六纵，俘虏兵一看自己的部队来了，立刻哗变了，张灵甫的炮兵阵地乱套了，华野很轻易地把张灵甫的重炮阵地占领了，掉转炮口向孟良崮山上轰。这时候张灵甫才知道自己犯了一个大错误，孟良崮是一座秃山，寸草不生，炮弹落地溅起的石头就能杀伤一片人。张灵甫最后坚持了三天，74师全军覆没了。陈毅、粟裕看到周边的国民党军围上来了，来不及打扫战场，连夜撤退。孟良崮地区的老乡说，战役打完之后三个月当地的老百姓不敢上山，漫山遍野都是尸首。蒋介石拿这个教训警告国民党，因此国民党用不了共产党办法。而共产党改造俘虏的经验每个野战军都有，抓住俘虏兵怎么办，抓住国民党军官怎么办，办法都是一套一套的。<br />
<br />
化敌为我是共产党的一个绝招，共产党能做到什么程度？当年四野的后勤是最棒的，后来我就问四野的老同志，当时四野怎么有那么雄厚的后勤，有军医院，又有军工厂，还有军事院校。四野的老同志说，四野里面的技术人员基本上是日本人，我听了大吃一惊。抗战结束时四野十万大军闯关东，没有带后方，没有带医院，而且八路军那时候也没有医疗设备。四平战役之后大量的军队撤到北满，撤到哈尔滨、齐齐哈尔、牡丹江，那么多的伤员没人治怎么办？四野政治部下了一个九九紧急指示，就地征招医务人员组建军医院。伪满时期东北医院中主要的医生、护士都是日本人。当时四野后勤部长指示干部去接收日本人的医院，把日本人的医生、护士强行征入解放军。当时找到哈尔滨最大一个医院，要名单，从名单里看哪个人是医生，哪个人是护士，然后到遣返日本人的难民营里抓，从火车上向下拖。当时一共征了8000多医生、护士，这些医生、护士一开始是被逼来给解放军治病的，解放军一看怎么给我治病的都是日本人呢？最初在军医院里没有一个日本医生护士没有挨过伤兵打的，这些人忍气吞声。黄克诚一看不行，咱们得讲政策，这些医生护士只要肯为我们工作，不管他们思想是否进步都应该给予优待。共产党的干部吃高粱米，给日本的医生吃大米，共产党的干部没有津贴，但是日本的医生护士按原来的待遇发工资，这样把日本的医生护士都争取过来了，后来这些日本医生护士跟着各个野战军参加了辽沈战役、平津战役、渡江战役、衡宝战役，一直打到海南岛。<br />
<br />
除了医生护士之外，还有军工。东北野战军迅速发展成为百万大军，而且华东野战军也需要炮弹，怎么办呢？于是在东北办兵工厂。当时解放军最大的军工基地在大连，大连当时是苏军占领的，国民党去不了，所以解放军在那儿秘密发展军工厂。我们都读过吴运铎写的《把一切献给党》，吴运铎说在东北建立军工厂就是在大连的甘井子，当年在那儿建立了一个炮弹厂。吴运铎说他和吴屏周厂长实验炮弹，有一发没爆炸，他们俩跑过去看，往那儿一蹲炮弹爆炸了，吴屏周当场被炸死，吴运铎受重伤。后来我到实地采访，看了当地军工厂写的厂史，我才知道那是因为我们的技术太差，这是一场严重的安全事故。因为当时我们这些土八路没有正规的金属加工技术，做的炮弹撞针有毛刺不光滑，所以拉不着。最后还是得用日本人，当时把在大连的日本军工技师集中起来，有的是制造火药的专家，有的是做炮弹壳的专家，还有冶炼的专家，把30多个日本专家集合起来构成了大连军工厂的技术骨干。另外，我们国家第一个航校，东北老航校，第一批王牌飞行员，包括后来的空军司令王海上将，都是日本教官教出来的，而且第一批女飞行员也是日本教官教出来的。所以不能不说共产党的思想政治工作厉害，能把日本人都教育成革命战士。<br />
<br />
这一段历史原来都是保密的，日本人大批遣返是在1946年，到新中国成立之后1953年又遣返一批日本人回国，其中大部分是我们共产党用的日本人，掩护身份让他们回国。回国的时候让他们把军装都脱下来，把档案都销毁，每个人得的奖章全部都收回。到了90年代，这些日本老兵陆续地回老部队探亲，总政治部下命令，重新做模子，给他们这些四野的日本老战士每个人发东北解放纪念章、平津战役纪念章、渡江战役纪念章，还要加一枚1955年授衔的时候发给营以上干部的解放奖章，重新发给他们。后来我采访这些日本老战士，他们都80多岁了，但是说起话还是40年代解放军的语言。对于解放军在解放战争期间化敌为我的能量、作用是应该刮目相看的。<br />
<br />
第三条，共产党能够充分动员群众支援战争，也就是我们所谓的人民战争。一提起这个，大家马上就想到车轮滚滚，当时确实是这样一个宏大的场面。我们正史上都说淮海战役“60万吃掉80万”，60万解放军吃掉80万国民党重兵集团，历史上可以这么说，但是账不能这么算。淮海战役是共产党以少胜多吗？根本不是，而是我们以压倒性的人数优势战胜了国民党80万大军。国民党的将领18军军长杨伯涛在双堆集和黄维一块儿被俘，他被押下战场，后来他在回忆录里写，他看到解放军的后方到处都是老百姓，有的给解放军推车，有的给解放军治伤员，有的给解放军做饭，他说我们在战场上哪儿看到过这种场面，国民党的后勤都是自己办的，自己拿卡车拉辎重，哪儿有这么多老百姓来支援战争？淮海战役先后动员的民工220万人次，加上60万共产党的正规军，你说比国民党的80万人数多了多少？<br />
<br />
共产党为什么能够动员这么多的人力物力？最重要的是共产党跟国民党根本区别在于，国民党是一个非常松散的组织，松散到什么程度呢？加入国民党很随便，可以集体入党；但是共产党不一样，共产党的组织渗透到解放区的每一个村庄，没有一点空白，每一个村子里都有党支部、村委会、民兵武委会、妇女救国会，最后还有儿童团，每一个村子里通过这五个组织把每一个老百姓都完全地控制起来，于是出民工都是组织上派的，而且各有分工。共产党能够充分利用民众力量组织起自己的战争机器。淮海战役、平津战役支援前线的民工都超过了百万。<br />
<br />
还有一个重要的问题，就是征兵。我们知道国民党主要是靠抓壮丁。有张照片是一个英国记者拍的，一个老头在国民党新兵里寻找自己的儿子。共产党这边都是骑马戴花、光荣参军，事实是否如此？后来我看了一些材料，感觉到人从本性上来说不愿意打仗的，农民更是如此。你看纪录片《人民的胜利》，说翻了身的东北农民在土改中获得了土地，他们志愿参军保卫胜利果实，骑马戴花参军了。后来我想还是不对，农民都是老婆孩子热炕头，分了地、分了房子、分了牲口，你叫他再去打仗，可能吗？从他个人来说是不会愿意的。后来看到一个材料，1948年初中共中央给东北局一个指示，要东北局在半年之内组建100个团的二线兵团补充东北野战军，因为林彪跟陈毅的情况不一样。在辽沈战役之前东北没有打过很大的仗，也没有俘虏过很多的国民党兵，所以毛泽东下令让林彪组织100个团的新兵，这100个团从哪儿来？从翻身农民中来。林彪当时下了一个指示，号召各地干部要当革命的兵贩子。怎么征兵？当时征兵的指标下到各县、各村，这个村里得征十几个兵，村支书、村长就把适龄青年叫到一起教育，这些青年都坐在炕上不说话、不表态，支书派人一个劲儿烧炕，炕上热的坐不住了，终于有一个跳了起来。好，这个算报名了，结果一个一个都跳起来了，就骑马戴花当兵去了。后来我看了张正隆的《雪白血红》，专门有一章“要当革命的兵贩子”，跟我写的一样，说明这不是假的。同样抓壮丁，为什么共产党的兵上战场敢打仗？解放军多打几个胜仗，这些新兵胆子就大了，就变成老兵了，人都是这样磨炼出来的。共产党在动员群众方面确实是有一套。<br />
<br />
共产党动员群众进行人民战争不光是参军，还有更狠的一招。我们都知道1948年上海的日子最不好过，物价飞涨，民不聊生，要粮食没粮食，要煤油没煤油，老百姓在银行前挤兑金圆券。不是蒋介石想让老百姓这样，最根本的问题是物资短缺，没有米、没有布，所以才涨价，所以奸商才囤积居奇。为什么缺？1948年初共产党基本上控制了东北、华北、江淮地区，国民党仅仅控制着长春、沈阳、北平、济南这样的大城市，这是什么含义？也就是全国100％的煤炭资源、80％以上的小麦产区、80％以上的棉花产区全被共产党控制了。解放区当时也知道国民党的物价飞涨，法币、金圆券天天贬值，共产党采取最原始的办法――自然经济，不用货币。农民都是以物易物，拿粮食换鸡蛋，拿鸡蛋换煤油，抵制伪币进入解放区。共产党还规定各种各样的政策，开了几个清单，一个是许出口的清单，一个是不许出口的清单，一个是许进口的清单，一个是不许进口的清单。什么东西许出口？古董、文物可以输向国民党统治区，那年头古董谁要啊？粮食、棉布不许出口。煤油、纸张、药品可以从国民党统治区输往解放区，因为解放区不生产这些东西。而不许进口的东西就多了，奢侈品解放区都不要。这样一下就把国统区捏死了，这才是真正的“农村包围城市”。国民党大城市没有物资来源了还不涨价，还不恐慌？上海最后狼狈到家了，1949年1月蒋介石一下野，上海人就公推杜月笙给毛公写封信，请求用上海的工业品换开滦的煤炭，毛泽东照准，要维持上海，不能让上海崩溃。在蒋介石下野的几个月，上海和唐山的开滦对开轮船，把煤炭拉到上海维持上海人的生计。国民党还不垮？根本没有经济基础了。解放区控制了物资，而且共产党能够控制得如此严密。<br />
<br />
共产党占了上海，上海商人又来劲了，不是物资短缺吗，我再涨价。结果陈云到了上海以后打经济上的淮海战役，知道上海的商人喜欢投机，陈云从东北、华北调运大量的粮食、布匹源源不断地往上海运，上海的商人一看大批的物资从公家运过来，敞开肚皮吃，没想到共产党用的是举国体制，用共产党那么大的解放区运那么多的物资进来供应上海，撑死这些商人。商人的资金都是有限的，把资金用完了，囤在仓库里等着涨价，但是共产党的布匹、粮食还是源源不断运进来，价格天天跌，跌到商人受不了的时候只好降价。共产党进上海之后给商人们一个下马威，叫他们知道了共产党的厉害。这不是政策问题，关键还是在于经济基础。共产党能够控制这些经济基础才能够打天下。<br />
<br />
最后一条，共产党的情报、渗透、策反无孔不入。在战争年代，情报太重要了，谁有情报来源谁就能打胜仗，这是一个显而易见的道理。国民党输就输在组织太松散，既没有政审也没有档案，无论是什么政府机关、机要部门用人，只要朋友一介绍，那就进来吧。<br />
<br />
所以在抗战期间，共产党就利用统一战线、国共合作的机会往国民党里派了大量的地下党、情报人员，那会儿任务是长期潜伏，不到关键时刻不醒。毛泽东转战陕北，当时中央可以走，但是毛不走，毛主席留在陕北，拖住胡宗南，说胡宗南也要听毛主席的指挥。后来有人写回忆录《胸中自有雄兵百万》，毛泽东在陕北坚持了一年多，其实毛是一个很谨慎的人。我参观毛的住处，最深的体会就是毛绝对不会拿自己的生命冒险。前一段我到甘肃哈达铺，毛泽东住的房子有一条小路直通后山，有情况随时可以掩蔽。西柏坡毛主席住的房子离防空洞最近，只要飞机一来一下就能进防空洞。毛泽东为什么敢于在陕北呆着不走？最重要的原因是，当时共产党有一个地下人员熊向晖，是安插在胡宗南身边的共产党情报人员。胡宗南身边的机要秘书是共产党，那还能有什么秘密？熊向晖在胡宗南进攻陕北之前就把作战计划通过情报网传到中共中央，所以中共中央对情况了如指掌，毛泽东就决定不走，转战陕北。毛泽东转战陕北的路线反其道而行之。胡宗南认为毛一定要过黄河，但是毛泽东却往西走，往安塞那边走，跟胡宗南兜圈子，为什么？这也是毛泽东算计好的。当时有这么几个条件，一个是黄土高原，那个地方最大的特点就是缺水，由于大部队一定要找有水的地方宿营，所以胡宗南的部队一出动，今天走几十里地在哪儿宿营是可以算的出来的，也就是必须要找一个有水的村子宿营。毛泽东的小部队昼伏夜行，就可以避开国民党的飞机侦查，而且电台保持静默，就可以避开国民党的技侦侦查。还有最重要的一条是，陕北老乡不会出叛徒，新区的老百姓闹不好就可能出叛徒，这是非常重要的。毛泽东是人，不是神，他是经过充分的酝酿考虑才决定在陕北不走。<br />
<br />
先说了共产党的情报工作，下面说说策反。越在关键时刻，共产党在国民党安插的钉子越是发生作用。当时共产党的情报策反系统有完整的组织，为首的是社会调查部，负责的是康生和李克农，下面分到各个野战军的政治部里有敌军工作部，是专门打入国民党的内线；还有城市工作部，专门在城里弄情报的；还有联络部，共产党的地下联络站，送情报的。这三个组织非常严密。解放军最近编了两套大书《中国人民解放军联络工作史》、《中国人民解放军敌军工作史》。1946年3月莱芜战役，当时济南的王耀武让李仙洲指挥三个军与张灵甫南线配合要把陈毅、粟裕包围在沂蒙山区，结果陈毅、粟裕决定先打莱芜。国民党46军军长韩练成跟共产党有关系，他问打起仗来怎么办？我们的联络员就说打起来你就临阵逃脱。韩练成这一跑不要紧，他的一个军就乱套了，李仙洲的三个军一下就都乱套了。陈毅他们的主力部队仅用一天时间就把国民党三个军五万多人全解决了。王耀武听说莱芜战役的消息以后大骂，他*的五万多人一天就完了，我就是放五万条猪也够共军抓一个礼拜的。到关键时刻给你搅乱了，尤其是在战场上，一点办法也没有。过了几年轮到王耀武本人，济南战役打王耀武，我军策反吴化文，吴化文不是王耀武的嫡系，王耀武让他守西线。王耀武在城里，让吴化文在城外，吴化文说这不是拿我当挡箭牌嘛。共产党策反吴化文的老婆，济南战役一打响，他在外围起义了，王耀武外线一下就是一个大缺口，还没有来得及补这个缺口，解放军就开始攻城了，很快就拿下了济南。还有淮海战役开始的时候，把守运河大路的国民党第三绥靖区的副司令长官何基沣、张克侠是地下党，抗战时期就入党了，就是共产党埋在国民党里的钉子，到这个时候他们俩起义了，一下把运河大路让开了。粟裕后来给中央写报告，说战机就是四小时。如果何基沣、张克侠不起义，我们在运河耽误四小时，就没有包围黄百韬的战机了。<br />
<br />
甚至在最高层，南京的国防部都有共产党的内线。共产党的情报厉害到什么程度？蒋介石还没撤退到台湾，我们的情报人员就已经到台湾了。当时最大的内线是国民党国防部办公厅的长官吴石。《老照片》公布过枪毙吴石前的一张照片。当时蒋介石到了台湾，国民党可以说是风雨飘摇，我们大量的情报人员已经渗透到台湾了。我看到的攻台准备的资料，一份是国民党军队驻防台湾一览表，还有一份是国民党高级将领在台湾住址一览表，当年国民党已经准备再次逃跑了，而且这些情报人员有的都半公开了，做高官的工作，策动起义，结果朝鲜战争爆发，我们攻台的计划搁浅了。蒋介石反过手来抓共产党，当时中共台湾党的负责人蔡孝乾叛变了，供出200多人，吴石这些人都牺牲了。我们的技侦、破译密码也非常厉害。早在红军时期共产党就有本事破译国民党的密码。后来毛泽东对军委二局的同志非常感激，在西柏坡的时候与他们合影，照片让中军委二局的同志站在中间，表示毛泽东对他们的尊重。当时这些破译密码的专家勤奋到什么程度？国民党的密码技术上非常复杂，但是有一个规律，即按照汉字的古韵排列，天天变化，为了掌握古韵，总参二局有一个干部能把整本的《康熙字典》背下来；还有一个干部为了研究蒋介石国民党军电报，都是文言文的，他专门研究明清的尺牍。谁说共产党里没有人才？<br />
<br />
战争是多方面的较量，但是总的结局是有它的必然性。我写《中国的1948年》就是希望给大家提供一些真正的史料，以后我会继续修订补充。看了这本书之后，有网友评论，这本书史料虽然细致，但是还有在五星红旗下立正敬礼的感觉。大家听了我的讲座之后，你们说说，我是应该在五星红旗下立正敬礼还是应该在青天白日旗下立正敬礼？<br />
<br />
谢谢大家！<br />
<br />
听众提问：<br />
<br />
解放战争一共死了多少人？<br />
<br />
刘统：<br />
<br />
说实在话，因为我们到现在为止没有一个非常详细的统计。过去的档案不集中，各野战军有各野战军的档案系统，没有向中央统一地报告。我在解放战争纪实中，每一个战役如果档案中有记载，我会写下来多少伤亡。但是如果说总的数字，因为还有很多小战役，目前没有确切的数据。<br />
<br />
观众提问：<br />
<br />
卫立煌和共产党有什么关系？<br />
<br />
刘统：<br />
<br />
卫立煌在抗战期间和共产党关系不错，但是卫在任东北剿总司令时和跟共产党没有具体的关系，要说关系主要是他的女婿汪德昭，汪德昭是我国科学院的院士，汪回东北之后给卫立煌当过一段时间的秘书，他还没有来得及跟我们的情报机关接上关系，国民党已经垮下来了。卫立煌因为有抗战时的关系，建国后还是从香港回来了。<br />
<br />
观众提问：<br />
<br />
很多人认为《雪白血红》这本书演绎的成分比较多，经常有人说信史还是应该看刘统的《东北解放战争纪实》。<br />
<br />
刘统：<br />
<br />
我到大连特意跟张正隆见面，我个人认为他是一位很正直的军人，他的作品《雪白血红》完全是用采访做成的，他采访了好几百位四野的老人。张正隆是军事文学出身，写东西有激情，从史学的角度看，他在排比材料、考证方面肯定会有疏漏。不过张正隆所说的每一个例子，包括部队的番号、具体的人都是真人真事。要从文学的角度看他，不能从历史学的条条框框约束他，《东北解放战争纪实》毕竟是历史的作品，跟他不一样。<br />
<br />
观众提问：<br />
<br />
在东北解放战争的时候曾经有日本军人在为解放军服务，我想问1945年以后苏联是否介入过东北的解放战争？有记载1950年以前苏联人确实介入过东北的建设，是否直接介入了东北的战争？<br />
<br />
刘统：<br />
<br />
杨奎松为这个事跟我争论过，他写《毛泽东与莫斯科的恩恩怨怨》，说共产党进东北以后就得到了苏联的援助，于是解放军就无敌于天下了，他是这个结论。我在《东北解放战争纪实》里有大量的篇幅说明共产党的武器是捡破烂儿捡来的。黄克诚有一句话，老兵老枪、新兵新枪，这是东北初期的状况。苏军撤离的时候对我们解放军没有任何的帮助。<br />
<br />
最困难的时候，在北满，林彪是跟金日成要了很多武器装备。金日成占了朝鲜之后武器全部换成苏式的，日本人的武器用不着了，所以转给了东北民主联军，据记载总共有三四百节车皮，这构成了解放战争初期我军的武器来源。到后来大局已定的时候，1948年年中，情况慢慢改变了，苏联派专家来帮助我们修铁路，提供了一些他们准备用来炼废铁的日式装备，还有一些捷克式的轻机枪，真正大规模的军事援助还是在中苏友好条约签订之后。<br />
<br />
观众提问：<br />
<br />
围困长春饿死很多人，这段史实现在是不是公开了？<br />
<br />
刘统：<br />
<br />
还是没公开。我在《东北解放战争纪实》中引用林、罗、刘给毛泽东关于围困长春的报告，在书中删节了相当一部分，就是说当时老百姓怎么痛苦，饿死多少人。长春国民党守军和解放军中间有一个死亡地带，国民党不让回去，共产党不让出来，后来共产党把老百姓从这个死亡地带放出来。最后统计，围长春的时候一共饿死10到12万的老百姓，这个数字郑洞国也认可，应该是差不多的。因为长春虽然有上百万人口，但是到兵荒马乱的时候大部分老百姓都逃难了，这个事毕竟是说明战争残酷的一方面。<br />
<br />
观众提问：<br />
<br />
请问共产党的情报工作。<br />
<br />
刘统：<br />
<br />
因为绝大部分事情都没有解密，所以我今天给大家讲的是零星见于回忆录的史料。比如熊向晖有回忆录，就可以讲。但是有很多做隐蔽工作的同志终生不会见诸于历史。当年做隐蔽工作主要的特征就是敌中有我、我中有敌，必须要跟汉奸特务打交道才能得到情报。建国后这些同志因为阶级斗争观念的影响，他们的作用也差不多了，于是他们就受到了组织上的怀疑。潘汉年这个案子牵扯到国内很多的隐蔽工作者、地下党，都受到了牵连。<br />
<br />
十一届三中全会之后陈云为什么专门提出给潘汉年平反，那时潘汉年和夫人都已去世了。陈云给中央写了彻底平反的报告，胡耀邦在批示中写了一段很有感情的话，他说，过去从事隐蔽工作的同志，他们的关系是非常复杂的，而且他们的行动也非常秘密，我们不能要求每一位做隐蔽工作的同志都能够提供他们的见证人和组织关系，如果这样的话今后就没有人再会为我们做隐蔽工作了。胡耀邦的这个批示也是对常年做隐蔽工作的同志给出了一个历史的评价。<br />
<br />
刘统，1951年生于北京。先后毕业于山东大学、复旦大学。历史学博士。1989年――2003年任中国人民解放军军事科学院研究员、《中国军事百科全书》责任编辑。现为上海交通大学历史系教授。已出版的著作有：《东北解放战争纪实》、《华东解放战争纪实》、《中原解放战争纪实》、《唐代羁縻府州研究》等。</div>

							
							
							
							
