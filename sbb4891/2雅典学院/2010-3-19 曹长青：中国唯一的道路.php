													<h2>曹长青：中国唯一的道路</h2>
						
						
																											<div id="postmessage_523168" class="t_msgfont">――曹长青：革命不仅可行 也是唯一的道路 <br />
<br />
革命，就是暴力革命；暴力革命就是大流血、大动荡；然后就会产生一个更残暴的政权。这是强调中国只能走“渐M改良”之路者的理论。这种思路的来源当然很容易理解。的确，共产革命不仅把中国人“革伤”了，也把“革命”这个词变成了滴血的人头，令人闻之发抖。<br />
<br />
<br />
毛泽东说了一句“枪杆子里面出政权”，革了所有人的命，吓得中国人，尤其是中国知识分子，再不敢提枪杆子；好像谁再提枪杆子，再提革命，谁就是毛泽东。毛泽东糟践了“革命”两字，于是反独裁的人就得“反革命”，否则就是“以暴易暴”。<br />
<br />
<br />
说实话，看有些人一再喊反对暴力革命，我总是想起西方左派们高喊的“天气过暖”。哪儿来的天气过暖？明明是为反资本主义制造出来的一个“说法”，就一本正经、义正词严地声讨。在中国哪来的“暴力革命”？如果有，就是共产党正在对百姓实行暴力革命，正在对言论自由实行暴力革命，正在对宗教自由实行暴力革命，正在对公平竞争实行暴力革命，正在对追求个人自由的渴望实行暴力革命。<br />
<br />
<br />
六四屠杀刚过的时候，人们一片反思：六四之前是知识分子期待共产党体制内自身反省改革，但六四的枪声粉碎了中国文化人的改良梦。可谁能想到，六四20年过后，在整个东欧都成为民主国家的20年之后，中国文化人又重新回到了指望中共“渐M改良”的老路上，回到了100年前依靠“皇上”变革的思维中！<br />
<br />
<br />
一个号称21世纪是中国人世纪的国家，可至今连被极端伊斯兰统治的伊朗那种选举都没有。在爆炸般的信息在空气中流淌的今天，中国人还需要翻墙挖洞，才能捕捉一点“自由”的符号；而司法的残暴、官商勾结的黑暗、社会的不公不义等等更充斥中国的每一个角落。但无论如何，宁肯委曲求全，宁肯当奴隶，也不要革命了？革命难道真那么恐怖吗？真如中国知识分子渲染的那样只有负面价值吗？ <br />
<br />
<br />
我在《当今中国有暴力革命吗？》和《用革命结束中共暴政》两篇文章中谈到，世界许多国家都援引美国《独立宣言》的精神，用武装革命推翻了专制：“当政府把人民置于专制暴政之下时，人民就有权利，也有责任和义务去推翻这样的政府。”请注意，人民不仅有权利，而且也有责任和义务，推翻暴政。<br />
<br />
&nbsp; &nbsp;当年统治美国的英国君主政权所做的，是一系列司法滥权和高税收，但却没有剥夺人民的言论自由、宗教自由和拥有武器的自由，更没有剥夺私有财产的权利。在那么“优越”的情形下，美国的先贤们都已经忍无可忍，发表愤怒谴责英国“暴政”的《独立宣言》，历数英国的一系列“罪恶”，然后拿起枪杆子革命。<br />
<br />
<br />
我们再看末代清王朝，在无知傲慢又跋扈的慈禧的皇朝天下，中国人仍有相当的言论自由、宗教自由和拥有武器的自由；中国人同样拥有私有财产的权利。但我们的前辈们也同样忍无可忍了，他们同样用武装革命推翻了大清王朝。后来的共产主义把中国推M更悲惨的一幕，但那不是革命的错，而是“人民利益”高于“个人权利”的共产意识形态之错！<br />
<br />
<br />
今天的中国，不如两百多年前的美国，不如一百多年前的中国。这个现状是否需要一个“根本的、性质的变革”？相信对这个问题的回答，改良派和革命派是同样的。异议出在怎样才能使这个“根本的变革”发生。从这个意义上来说，所有反专制的人，大家都是认同“革命”的。<br />
<br />
&nbsp; &nbsp;只不过“改良派”认为，只有靠共产党体制内部的松动，共产党高层的变化，才能带来中国政治体制的根本变化；而要指望党内变化，就得说“党”听得M去的话，就得连捧（人权有M步）带哄（我不跟你作对）再加上谏（党的宪法还需要修）。而号召革命是不现实的：一是没有可能，因为老百姓手里没有武器；二是革命会有大流血，大动荡，付出很大代价。换言之，在今天的中国，改良都推动不了，革命的本钱在哪里？<br />
<br />
&nbsp; &nbsp;表面上看，这种思维似乎很有道理。苏联由于产生了戈尔巴乔夫而垮台，波兰雅鲁泽尔斯基放权，台湾蒋经国开放党禁报禁，后李登辉推动总统直选，走向宪政民主。这些都不仅是体制内，而且是独裁政权的最高领导人发生了变化才导致了“革命”性地改变了政权的专制性质。<br />
<br />
<br />
但是，如果深入了解苏联、波兰、台湾的情况，会清楚地发现，它们的民主转型，都不是改良派规劝、恳求、期待掌权者的结果，而全部都是靠体制外的抗争和压力，最后导致的体制内最高领导层迫不得已的变化。<br />
<br />
<br />
苏联到戈尔巴乔夫掌权时，索尔仁尼琴等知识分子长期传播的“共产党是邪恶”，必须结束共产党统治的思想，已经在俄国相当深入人心，俄国产生了一大批持同样想法的异议知识分子。他们虽然被抓被关，但仍前赴后继，发出声音。像苏联氢弹之父萨哈罗夫提出“一个人不能同时坐（支持和反对共产体制的）两把椅子”；他公开跟共产党决裂，偷运到《纽约时报》发表的文章，被称为“苏联极权制度的判决书”。<br />
<br />
<br />
正因为必须结束共产统治的声音被越来越多的俄国人听到、认同、共鸣，于是俄国人民对当局的不满，蔓延全国。正是在这种外部压力下，戈尔巴乔夫为了缓和社会矛盾，才提出《新思维》，不得不做一些改革，目的是要保住共产党的权力。<br />
<br />
<br />
于是，在偏远之地遭隔离软禁的萨哈罗夫被戈尔巴乔夫亲自请回莫斯科，后M入国会当议员。如果是中国知识人，得到共产党开明领袖的如此“皇恩浩荡”，早就感激涕零，拥护“明君改革”了。但萨哈罗夫在国会开幕时，虽被戈尔巴乔夫安排第一个发言，但他却根本不配合戈尔巴乔夫的“新思维”，而是明确提出，要结束共产党的专制制度。连戈尔巴乔夫都不得不回击他的批评，最后两人决裂。<br />
<br />
<br />
十多年前我在《知识分子和共产党》一文中指出，正是索尔仁尼琴、萨哈罗夫等俄国异议知识分子长期传播的共产党是邪恶、必须结束共产统治的思想深入人心，所以俄国人在有机会的时候，不再是寻找党内的“胡耀邦、赵紫阳”，而是给共产党寻找永久的墓地。后来叶利钦敢于站到路障上振臂高呼，抵制苏共顽固派“九人帮”，就是建立在对俄国人要推翻专制的信心上，结果他成功了。换句话说，你的目标不是寻找“中国的戈尔巴乔夫”的时候，才会有戈尔巴乔夫出现！<br />
<br />
<br />
波兰的情况比苏联还典型。人所共知，是以团结工会为主导的长期抗争的结果。他们一开始面临的全都是镇压，瓦文萨等领导人几乎全部被抓被判，但他们一直顽强地跟共产当局对抗。最后波共领导人雅鲁泽尔斯基面对强大的民间压力，处于相当的困境中，迫不得已，才跟团结工会谈判，同意全国大选，结果体制外派赢得了全部自由选举的席位，波兰走向民主。波兰的经验同样不是改良派期待、恳求、规劝统治者“开恩”的结果。<br />
<br />
<br />
台湾的情形更是如此。这些年我对台湾的民主转型过程做了些研究，一个清晰的结论是，国民党高层的变化，完全是靠“党外运动”压力促成的。台湾人民自五十年代就开始体制外的反抗，有地下结社抗争、党外刊物《自由中国》、六十年代彭明敏发表《台湾人民自救宣言》、到七十年代风起云涌的党外运动、美丽岛事件直接和政权冲突，直到1986年民M党的成立、壮大等等。这些都是促使蒋经国转变的决定性因素。这期间无数人被关M监狱，逃亡海外，甚至被屠杀。民M党是在国民党仍没解除戒严令、没有开放党禁报禁的情况下成立的，他们当时做好了全部都去坐牢的准备。除此之外，蒋经国访问纽约时还遭到台湾人刺杀，给了他一个很大的震惊。这些来自体制外的越来越强烈的压力，促使蒋经国思考，到底怎么做才能赢得台湾的人心、才能继续维持国民党的统治？蒋经国选李登辉做副手，绝不是要放弃国民党的统治，而是为了在安抚台湾人的同时，巩固国民党统治。蒋经国时代，李登辉一直都是非常忠诚的国民党人。但蒋经国并没留下让李登辉接班的遗言，他至死都没想好国民党怎么才能撑得下去。<br />
<br />
&nbsp; &nbsp;李登辉推动总统直选一事，尤其说明问题。我曾以为李登辉完全是出于推动台湾民主的愿望，所以顶着国民党内部的巨大压力，强力推行了总统直选，一举完成了台湾的和平民主转型。我曾对此极为欣赏，并撰写长文热烈推崇。但后来由于曾准备写一本李登辉的传记，阅读了更多关于那个过程的资料，加上对李登辉本人以及其他相关人员的采访，得出的结论是：李登辉作为台湾人，有相当的本土意识，也有一定的民主理念。但是，他首先是为了保住自己的总统位置，才推动了总统全国直选。为什么？<br />
<br />
&nbsp; &nbsp;李登辉上台后，由于他是台湾人，在国民党内位置一直不稳，所以他说自己是“虎口中的总统”。而体制外抗争的一大批人，在反抗国民党的同时，又在相当程度上支持李登辉，指望靠他的力量，使国民党降低对党外抗争的镇压。于是在客观上产生了这样的效果：体制外抗争一方面强化了李登辉在民众中的影响力，另一方面又使国民党内部强硬派对李登辉的反弹更大，取代他的党主席和总统位置的势力也越来越强。在这种情况下，如果总统仍一如既往地在国民党内选，李登辉就完全可能输掉；而如果M行全国普选，他反而胜选的系数更大。因为全民投票，虽然会有民M党人出来竞争，但相当一部分体制外的抗争者担心民M党还不够成熟，胜选机会不大；与其输给国民党强硬派，不如选国民党里的亲台湾派。这就是为什么类似绿营政论家金恒炜等一大批完全是体制外抗争的势力，在1996年并没有把票投给更符合他们理念的、民M党候选人彭明敏，而是投给了国民党的李登辉。<br />
<br />
<br />
对李登辉来说，推动全民直选，他既可以保住总统位置，又得到了推动民主的心理安慰。几乎所有统治者，首先想到的都是保住自己的位置。但他们同时需要良心平衡，认为自己是站在人民一边，站在潮流、道义一边。所以体制外反抗国民党的压力越大，就越使李登辉清晰民意的浪潮，更给他改革的底气；同时，体制外反抗越强烈，国民党强硬派也越要排斥掉李登辉（像苏联高层九人帮要拿掉戈尔巴乔夫一样），导致他必须依靠体制外力量而保住自己的权力。 <br />
<br />
&nbsp; &nbsp;相反，如果台湾没有那么强烈的体制外反抗，那就只有两个结局：第一，李登辉如华国锋一样被干掉；第二，李登辉如江泽民、胡锦涛一样，稳住了自己在党内的地位，然后像江泽民一样光荣退休，连战接班，国民党一如既往地统治下去，现在也轮到马英九，下面再接着郝龙斌。而两党政治就会像今天的中国一样，处于遥不可及的状态。<br />
<br />
所以，从苏联、波兰和台湾的经验来看，目标清晰的体制外抗争是结束专制的必须条件，而绝不是可有可无的。不少改良派观点的人，也是认同上述理论的，也认为要靠民间、靠体制外压力促使体制内改革。但同时又感觉靠体制外太慢了，因为体制外的力量太薄弱了；靠那些手无寸铁、没权没势的老百姓，得到猴年马月才撼得动中共那颗大树？<br />
<br />
<br />
那么到底哪头更慢？我认为是寄托中共党内改革更慢。因为期待体制内改革，就基本是等待。人类有什么目标是靠等待可以达到的呢？只有死亡。统治者都期待不变，而被统治者当然希望变化。所以，必须依靠希望变化者，而不是依靠要保持不变者。这点即使在民主国家都是如此，更何况在独裁国家。<br />
<br />
<br />
而把目标放在体制外、民间，就是去点燃火种。变革的火种永远在百姓这边，而不是在占据统治位置者的一边。无论在苏联、东欧，还是台湾，人家全都是点体制外、民间这边的火种，最后火烧到那边，烧到体制内，烧到党内高层感觉疼，不改不行了，而且是“不敢不改”了；得靠改革保自己（像戈尔巴乔夫和李登辉那样），而不是靠顽固守住现状而保自己。<br />
<br />
<br />
永远不要把走宪政之路的希望寄托在专制政权高位者的“民主理念”上，那个最后促使“革命”成功的中国高层领导人，一定是一个不得已的明智者。但要促使他“不得已”，必须是抗争一方的力量足够大。而要使体制外抗争的力量足够大的前提，是有清晰的、必须推翻中共政权的理念，而不是改革它。<br />
<br />
<br />
恰恰是中国知识分子一路对共产党一厢情愿的改革期待，才导致中共对八九民运的镇压；恰恰是学生在人民大会堂下跪那个标志，给了共产党做皇上的理直气壮；恰恰是“没有敌人”这种完全错误的思维，才被暴政一次次屠杀；恰恰是一直占据绝对话语权力的中国文化人，把要依靠党内改革的思维传播得深入人心，导致人人都相信，没有什么力量可以代替中共，所以才导致那个政权今天依然稳定。那种要依赖党内改革的思维，无论主观意愿如何，在客观上起到的作用，是呼应了中共要稳定、要所谓和谐社会的路子。<br />
<br />
<br />
真实是：只有人人都意识到专制是不可改变本性的狼，是自由的敌人，革掉它的命是唯一的出路，民间的力量才可能起来；而只有要起来推翻中共政权的气势出来了，才能使高层中共领导人被民众的气势威吓住，不敢再使用武器，才能在避免流血的情况下，迫使统治者让步。八九民运，人民有暴力吗？为什么遭到了血洗？暴君什么时候因为你下跪，他就不砍你的头？恰恰是因为他看惯了你的下跪，觉得你就应该下跪，所以杀你的时候不在乎。因为他知道，杀了你，更多的奴隶跪下来。邓小平不是说，杀二十万稳定二十年吗？没错，都不用杀二十万，连二万都不用，不就稳稳当当地统治了二十年吗？判一个刘晓波，看你们谁还敢再联名！这难道不是眼前的事实吗？<br />
<br />
<br />
有人说，既然连联名发个呼吁、谏言都没法做了，还谈什么革命，还有什么路可走呵？当然有！人民是沉默的大多数，他们不见得需要有什么行动，但只要“专制必须推翻，中共绝不可依赖”的种子深入人心，一旦机会来了，就不会再是八九民运的要求党内改革，而是从一开始就追求根本的、性质的变化――改朝换代，建立一个真正的宪政民主中国！<br />
<br />
<br />
八九民运之前，在中国根本没有任何大规模的反抗组织和运动，但胡耀邦去世后不到一个月，不仅北京发生了包括社会各界人士的、几百万人上街的大规模抗议活动，而且是全国各地都同时爆发了声势浩大的抗议活动。<br />
<br />
<br />
这个效果的产生，既是民众对长期中共专制怨气的发泄，也是八十年代中期，知识分子们向往西方，传播西方自由思想的一个结果。所以一有机会，民众一下子就起来了。当时知识分子的主流倾向是亲西方的，同时也是寄托党内改革派的。所以八九民运的诉求，没有超出知识分子思维的框架。如果当年知识分子一路传播的是一党专制必须结束的信息，八九民运就可能是不同的结果。<br />
<br />
<br />
当年由于时空限制，导致知识分子思想的局限，再加上全世界的共产政权都还没垮台，也没有前车可鉴，所以还有情可原。但是今天，在六四屠杀20年过后，在全世界主要共产国家都崩溃之后，在信息传播如此发达的今天，中国知识分子再回到20年前、100年前的依靠皇朝体制内改变的老路，是不可原谅的。<br />
<br />
<br />
革命不仅有路可走，而且是唯一的可行之路。需要的是点燃火种，传播到位的、共产专制必须结束的信息；传播自由的美好，自由的价值；一旦机会来临，貌似庸庸碌碌的大众、墨守成规的社会各个阶层，都会在一夜之间，成为追求自由的战士。<br />
<br />
<br />
有人认为中国人种有问题，需要几百年殖民地。我绝不这样认为！自由的渴望在每个人心中，中国人绝不例外。一百年前，我们的前辈吹响过革命的号角，举起过推翻专制的旗帜，他们曾经胜利。今天，在我们这一辈，同样会有人吹响这只号角，举起这面旗帜。我们不仅同样会胜利，而且在当今世界大潮的推波助澜下，我们会彻底推倒中国五千年专制的万里长城！<br />
<br />
&nbsp; &nbsp;2010年3月17日于美国</div>

							
							
							
							
