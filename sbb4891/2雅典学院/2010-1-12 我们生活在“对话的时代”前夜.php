													<h2>我们生活在“对话的时代”前夜</h2>
						
						
																											<div id="postmessage_427381" class="t_msgfont">【这两篇文章送给[再世关羽]等情绪易激动的朋友。如作者所说：我们生活在“对话的时代”前夜，这样的守则对我们尤为珍贵。】<br />
<br />
<strong>傅国涌：我们需要这样的“对话守则”</strong><br />
<br />
不久前，《t望》周刊2007年第46期做过一个“十七大纵深报道”的专题，其中有篇文章列举了当前中国社会矛盾的一些特征，比如社会矛盾显形化、矛盾冲突群体化、矛盾博弈政治化等等，文中说，“转型期一个简单的问题往往会演变为复杂的社会问题。矛盾个体往往不直接要求解决自身问题，而将问题与转型期的腐败、人权保障等政治问题相挂钩。”我基本上同意这个分析，但对于“矛盾博弈政治化”这个说法，我有一些不同的看法。<br />
<br />
在我看来，“博弈”这个词是和讨价还价、相对公开、公平的规则联系在一起的，你下一步，我下一步，有对峙，也有妥协，有得寸进寸，也有让步后退，如果一方独大，处于强势的利益群体和弱势的利益群体之间是很难有博弈可言，有之，也是强势的恩赐。尤其是在强势利益群体几乎完全主导了规则制定权，没有制定也没有迹象表明他们愿意制定出与其他利益群体博弈的规则时，我们不能不谨慎地谈论博弈一词。缺乏有序的、有规则可依的对话机制，博弈只能是无序的博弈。再说，“ 博弈政治化”，我觉得博弈政治化只有在社会存在对话机制或者出现这种契机时，才可能性存在，否则，连真正的博弈都没有，又哪里来的博弈政治化？<br />
<br />
“对话”这个词，20年前曾写进了中共13大的政治报告，一度是个热门新词，在中国人的政治生活中，当时，这个词缺席已久。但在近代以来的中国历史上，以 “对话”方式解决重大问题，尤其在历史的关键时刻，曾经给我们这个古老民族带来过几次新的机会。1911年冬天到1912年春天，在袁世凯代表的北方实力派代表和孙中山代表的南方革命派之间，曾经有过一次以“南北和谈”为名的对话，双方联手达成了把满清朝廷送进博物馆的结果。1919年前后，在一片纷乱的军阀扰攘声中，南北各派政治力量的代表，也曾经在上海的会议室里坐下来，以对话方式试图解决中国的南北分裂、军阀割据问题，尽管没有成功。1945年秋天，抗日战争的胜利给本民族再次带来一次空前的契机，毛泽东从延安到重庆，毛蒋会谈，就是一次政治对话，为随后的政治协商会议铺平了道路。在1946年初拉开帷幕的政协会议，可以说是一次真正意义上的政治协商，坐下来的对话不仅仅是对立的双方，还有第三方，而且第三方之间的观点、取向也是多元的，民盟代表与青年党代表就有不同，无党派社会贤达之间也各有自己的立场，38个代表，在重庆山城的对话、博弈曾经牵动着全国许多人的关心。要说博弈政治化，我想，那一次就是成功的政治博弈，以和平的、理性的方式，通过对话解决彼此之间的矛盾、分歧，各自代表各自的利益、价值，桌子上针锋相对、唇枪舌剑、各不相让，但是谁也没有一口把对方吃掉的可能，最后不能不达成妥协、平衡。对话的魅力、妥协的魅力、博弈的魅力，在1946年春天到来之前，尽展无遗。口才便捷、学养深厚的罗隆基、王若飞、王世杰等人，都在那一刻成为中国的政治明星，他们的才情，他们的风度都进入了历史。<br />
<br />
对话被暴力取代，是中国的悲剧，背后有复杂而深刻的历史文化背景和现实原因，不能否认的是，中国人也曾经选择对话方式来解决政治争端，解决利益纷争，而且不止一次。20年前，“对话”这个说法在官方文献出现时，完整的表述是“社会协商对话”。18年前，我们这一代人怀抱的全部梦想，就是希望以“对话”方式，以公民身份公开表达自己的意见。“对话”的观念，当时已深入许多关心民族命运的大学生、知识分子心中。最终没有能进入实质性的对话，掌握绝对权力的一方当然负有绝大部分乃至全部的责任。但这样说并不意味着处于弱势的民间一方就没有任何可议、可思的地方，在对话的大门没有完全关死之后，在非实质性的对话机会还存在的情况下，在对话现场的表现，今天看来仍有许多值得反省、检讨之处。当时，民间有某些代表的激愤、夸张情绪，显示出来的幼稚、天真和不成熟，在某种意义上预示着我们这个民族离通往一个“对话的时代”还有相当距离。一晃18年过去了，“对话”这个词也早已退出我们的政治生活、社会生活，中国仍处在一个“公开信的时代”，但是，我相信，“公开信的时代”终将被“对话的时代”取代。因此，讨论“对话”这个概念是必要的而且是迫切的。<br />
<br />
在18年前的那个冬天，哈维尔等捷克知识分子在布拉格成立了“公民论坛”，制定了8条《对话守则》，在街头巷尾、居住区、大学校园到处张贴，希望广为人知，以便监督。 “公民论坛”提出的对话守则：<br />
<br />
一、对话目的是为了寻求真理，而不是为了竞争；<br />
<br />
二、不要作人身攻击；<br />
<br />
三、保持主题；<br />
<br />
四、辩论时要用证据；<br />
<br />
五、不要坚持错误不改；<br />
<br />
六、要分清对话与只许自己讲话的区别；<br />
<br />
七、对话要有记录；<br />
<br />
八、尽量理解对方。<br />
<br />
大约10来年前，当我第一次看到这8条守则时，真的是耳目一新，内心极为震撼。8条守则简单明了，而且很实用，它不仅是用来约束自己这一方，同时也希望对话的另一方遵守。只有成为双方共同信守的规则，这样的守则才是有效的。这个对话守则实在太好了，值得人人学习，大力推广，特别是有一些守则恰好是我们习惯的思维方式中所缺乏的。<strong>无论是强势利益群体，还是弱势利益群体，归根到底都要学会对话，学会在对话中生活，而不是在仇恨和暴力中生活，对话是最佳的选择。学会对话，就是要告别自说自话，告别赢家通吃的意识，告别以强凌弱、强词夺理，当然也要告别仇恨意识、敌人意识，逐渐就我们只有一个中国，共有一片大陆，共有一个时代这些观念形成共识，在包容克制中化解矛盾。有矛盾并不可怕，矛盾也许会永远伴随着人类社会，重要的是我们面对矛盾的姿态，学会在对话、博弈中不断地解决矛盾，这个过程就是社会进步的过程。哈维尔他们制定的这8条对话守则，为我们提供了一个活的范例。我们生活在“对话的时代”前夜，这样的守则对我们尤为珍贵。</strong></div>

							
							
							
							
