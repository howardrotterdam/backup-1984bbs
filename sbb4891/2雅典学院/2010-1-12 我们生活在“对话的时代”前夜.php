													<h2>郭宇宽：辩论从学会讲道理开始</h2>
						
						
																											<div id="postmessage_427385" class="t_msgfont">2003年九月下旬的北京，两年一届的国际大专辩论会又拉开了序幕， 1986年中央电视台第一次转播亚洲大专辩论赛让中国老百姓见识到一种“在电视上吵架”的活动，而在93年首届国际大专辩论会上复旦大学的夺冠掀起一阵辩论热潮，距今已是整整十年。在今年这短短的十天内来自世界12个不同地区地的青年学子用华语展现了思考和表达的水平，赢得鲜花和掌声。作为本次东道主代表队的中山大学代表队，更是下了血本，集中庞大的顾问教练班子，提前一年就开始准备集训，学校方面对次一路绿灯，几乎当做头等大事来抓，很有势在必得的架势。非独中山大学，前几届参赛的大陆高校大体如此，莫不把在中央电视台露脸辩论队当作自己的门面。<br />
　　<br />
　　但这些年从社会特别是网上舆论来看，辩论活动获得的美誉度远没有关注度高。很大程度上在国人眼中辩论始终被视为一种小儿科的抬杠，毫无意义的口舌之争，尤其某些自认为有些文化的人更常以对这样的活动不屑为荣。曾有南京大学的一位女辩手，回学校后因受到这样的压力而表示“以后再也不辩论了”，有不少热爱辩论的朋友也曾向我表达过类似的苦恼。<br />
　　<br />
　　这样的苦恼表面上是辩论赛的问题，实际却不能孤立的来看，它背后折射的是整个社会文化氛围甚至就是社会本身的问题。<br />
　　<br />
　　<strong>在我们的文化场中，几千年来不同的声音是可怕的，中国人从小生活中就有太多不容置疑的权威和预先设定且不容争议的结论。一个人的双脚被束缚的太久，即使松绑以后，也要很长时间才能找到走路的感觉。</strong>所有这一切折射到大学生辩论赛中，便是对真问题的回避，没有真问题便不会有真思考，辩论赛失去了思维碰撞的灵魂而退化为浮华的电视秀。<br />
　　<br />
　　早年的国际大专辩论会中对其他国家地区的代表队有一个公认的评价就是“比较老实”有问必答，当年大陆代表队有一句套话就是“对方同学问我们……，我倒想请问对方辩友……”，辩论场上看不到对于对方质疑的真诚回应，充斥着“黑夜给了我黑色的眼睛，但我却注定要用它去追求光明”之类，由教练事先设计，可以用在几乎任何场合的诗歌背诵表演和掉书袋，就这样倒也所向披靡。辩论场由一些擅长朗诵和背诵的人占据，他们负责把集众多队友教练之功的辩词在现场表演就可以了，辩论成为清谈和闲谈，这样的辩论赛，调动的是辩手的口腔肌肉而不是头脑，除了一开始能给公众带来一点新鲜感，看多了不厌倦才怪。从这样的辩论赛中走出了很多精美的男女花瓶，他们的去处都不错，不少人被陈列在各大电视台的银屏里，忠实的继续愉悦大众，这样也足以让很多人羡慕。<br />
　　<br />
　　一颗种子在不同的土壤里有时会开出不同的花朵结出不同的果实。辩论赛在美国则是另一番情景，如果看一看那里的大学生都在辩论些什么，简直要让我们瞠目结舌，以全国校际辩论（ceda）为例，其中涉及都是以下类型的题目：<br />
　　<br />
　　1971-1972[美国是否应该全部撤出驻扎在西半球以外的军事基地的所有地面作战部队]<br />
　　1984－1985【美国进行总统大选的方式是否有害于民主】<br />
　　1987－1988【美国司法系统是否过分强调出版自由】<br />
　　1988－1989【总统大选中第三大政党的假如是否有理于政治进程】<br />
　　1992－1993【联合国人权宣言的履行是否比保护国家主权更加重要】<br />
　　<br />
　　这些都是典型的“肉食者谋之”的事情，轮的着平头百姓来操心么，何况还这么大精力组织几个乳臭未干的毛孩子，他们懂什么呀？<br />
　　<br />
　　而美国辩论研究会（afa）有自己的看法，他们认为人们通过公开演讲、讨论和辩论等活动，对公共事务自由交换看法和客观评价各种思想，是维持一个民主社会的基石。辩论活动为公民有效地参与到一个民主社会中去做准备。宪法规定言论自由，而议会、法庭和大多数私人组织都把辩论作为行动工具。因为辩论在各级决策机构中广泛应用，所以公民如果没有辩论知识，合理有效地使用言论自由权，表达自己的看法和要求，公民权力便仅仅停留在纸上。<br />
　　<br />
　　在它的宗旨中有这样的内容：<br />
　　<br />
　　辩论活动应该为当代重大问题的深入细致的调研创造机会。<br />
　　辩论活动应该在解决这些问题的过程中促进逻辑推理的应用，并最大限度地利用可得到的证据。<br />
　　辩论活动应该训练学生清楚有力地选择、安排和组织资料的能力。<br />
　　辩论活动应该训练学生真诚和具有说服力地把这些资料呈献给观众。<br />
　　<br />
　　他们相信历史的教训，不运用或不充分运用的自由权利不久就会丧失。受过辩论训练，善于且勇于表达的公民才有希望真正有效地参与到一个民主社会中去。<br />
　　<br />
　　在这样的传统下，一些时间就是金钱的西方国家，从谈恋爱到上床恨不得只用五分钟，用在辩论上的时间却毫不吝惜。打官司要辩论、议会开会要辩论、连选总统这种正经大事都要辩论，在中国人看来简直是视同儿戏。<br />
　　<br />
　　还是以美国为例，一九五九年的总统选举日（十一月三日），一些从事演说教学的教授开始组建&quot;总统竞选辩论委员会&quot;，号召总统候选人以1858年传统的林肯-道格拉斯辩论形式进行面对面的辩论。这一提议很快得到了美国公众的响应，不久出现了一批支持这一主张的社论、专栏和文章。电视网表示他们愿意合作。一九六零年九月二十六日，参议员约翰f・肯尼迪和副总统里查德m・尼克松进行了第一次总统竞选辩论。在竟选期间他们共进行了四次辩论，《纽约时报》称这几次辩论为选举中&quot;真正决定性因素&quot;。<br />
　　<br />
　　当公众期待着总统公开辩论，便没有哪一个候选人能够拒绝参加。在一九八零年的总统竞选期间，卡特总统自持身份拒绝与里根及第三党候选人约翰・安德森进行一场三方辩论。里根同意与安德森辩论，并因&quot;风格高&quot;而得分；相反，卡特因&quot;没有风度&quot;而失了分。在后来的竟选中，卡特不得不极不情愿地答应与里根进行一场辩论。大选之后，里根和卡特的支持者及许多独立的评论员都称那次辩论为选举中的&quot;转折点&quot;和导致里根以绝对多数获胜的重要因素。<br />
　　<br />
　　这一活动逐渐成为了一项传统，1988年共和党候选人副总统乔治・布什和民主党候选人迈克尔・杜卡基斯90分种的电视竞选辩论中，双方必须按规则在规定的时间内回答事先不知道的任何问题。cnn的节目主持人伯纳德・肖要求双方在两分钟限定时间内回答超过时间就要被打断的前两个问题是这样的：“州长，如果基蒂・杜卡基斯（杜卡基斯夫人）遭到了奸杀，您是否赞成坚决判处杀人犯以死刑呢？” 问布什的则是“现在我引用宪法第二十次修正案第三款：&quot;如果在规定的总统任期开始之际，当选总统故去，当选的副总统将成为总统&quot;意思是说如果你当选总统而在就职日前故去，自动地--丹・奎尔会理当成为美国的第41届总统。你对那种可能性有什么可讲的吗？”杜卡基斯因为回答的都是冷冰冰的套话而在选民心中的形象大打折扣。<br />
　　<br />
　　而在接下来的1992年，在任总统布什在第一场辩论中揭民主党候选人克林顿的伤疤，指责当年他在英国组织反对越战示威的行为，说克林顿是在外国的土地上反对自己的国家，并且称这不仅是一个爱国主义的问题，而是人品和判断能力的问题。克林顿冷静而又智慧的回应：&quot;当麦卡锡在国内到处攻击人们缺乏爱国主义精神的时候，他是错的。当时一位来自康涅迪格州、名叫普雷斯科特・布什的参议员，也就是你的父亲，挺身而出和麦卡锡做斗争。你父亲和麦卡锡斗争是对的。而你对我的爱国主义精神提出的批评则是错的。我虽然反对越战，但我却热爱自己的祖国。&quot;一举赢得了选民的理解和尊重，并令海湾战争中踌躇满志的布什黯然失色。<br />
　　<br />
　　如今总统竞选辩论已成为美国政治舞台上必不可少的一幕。在全国观众的众目睽睽之下，展现自己对公共政策和美国价值观的理解，总统候选人不仅思考和表达能力甚至风度人格都在质疑与挑战中经受公众的检验和评价。公开辩论为公众营造多向思维的环境，做出成熟的判断，一个领袖要让公众走向他所指引的方向，只有通过摆事实讲道理的唯一途径。如果没有一个讲道理的平台，按照权术的逻辑，林肯这样无凭无恃的穷律师不可能成为合众国的总统。<br />
　　<br />
　　从某种意义上讲一个社会走向文明是从学会讲道理开始的，当一个家长开始和自己的孩子讲道理时这是一个文明的家庭，当一个政府开始和老百姓讲道理时这是一个文明的政府。而当一个社会从每一个细胞里都崇拜权力的时候，它根源出于对权力的恐惧，在一个用拳头和枪杆子来制定规则的社会里，丛林逻辑以外的一切都是 “空谈”，无论圣雄.甘地还是马丁.路德都会显得象一个喋喋不休的迂腐白痴，马踏河山的铁血枭雄才是真正的智者。在这样的文化土壤里，其实不仅辩论赛，所有和讲道理有关的行为都往往被会视为“书生气”甚至“迂腐”的。<br />
　　<br />
　　前不久有这样一则令人刺目酸心的报道，孙君志刚的父亲对记者痛心地说，他后悔让孙志刚上大学，要是少念些书，就少明白些事理，也就不会硬要讲道理而跟人&quot;顶了嘴&quot;，就不会死了。象孙志刚父亲这样善良的中国老百姓在生存经验中往往不相信讲道理会有好处，但给逼急了会找领导“评理”，“讨个说法”，因为“理”不是讲出来的，而掌握在权威手里。秀才遇见兵，并非有理说不清，而是有枪杆子在手的人常常可以不讲理，没有权力的人讲道理也没用，甚至会惹来麻烦。这样的环境熏陶下，富有生存智慧的中国人便一代一代讲道理的能力逐渐退化，甚至爱讲道理的习惯都被视为“祸从口出”的缺点而从小加以纠正。<br />
　　<br />
　　今天的中国正在发生着显著的转变，一种开放的观点市场正在形成中，众多有不同思考角度和思维方式的意见领袖们，把各种议程带入公众视野，帮助公众全面的认知问题，并在政府决策和广大公众意见的良性互动关系中发挥媒介作用。从电信改革到三峡工程，从9.11事件到磁悬浮技术采购，中国老百姓又开始找到了谈论国是的热情。<br />
　　<br />
　　但如果留心的话，往往会发现在这一过程中，包括知识阶层中很大一部分，甚至是很著名的学者、文化人，都还没有形成良好的讲道理的习惯，意见相左的双方常互斥对方“小人 ”、“伪君子”、“卖国贼”，理不直而气壮的言论比比皆是。在这个角度上就不难理解为何在一些西方国家教育系统辩论的训练如此重要。<br />
　　<br />
　　套用一句话“讲道理要从娃娃抓起”。前不久我遇到了一位美国irvine valley college口语传播学系的系主任gary rybold教授,这位老先生是位辩论迷，他指导的队伍曾数次获得美国大学生辩论联赛的冠军。在交流中，反复解释后这位老先生还是有两件事很不理解，一是中国的大学生辩论赛为什么都是由电视台发起的，二是为什么中国大学生参加辩论为什么都要由学校来出面组织。他说美国有多的数不清的大学生辩论赛事，都是由各高校自发组织的，只要凑上几家愿意缴纳会费，就可以拉一杆大旗，组织上一个辩论联合会，全国搞巡回赛，各家轮流坐庄，大多数都没有电视转播，主要重在参与。而且美国年轻人参与辩论，非但不需要学校下力气组织，而且需要自己承担颇高的训练费和聘请指导老师的费用，就这样仍然十分踊跃，从小学、中学就开始了。<br />
　　<br />
　　<strong>如果说现代辩论比赛的有什么益处，最重要的不是眼前被发现的它能给学校和个人扬名，而在于它深层的文化意义。它从根本出发点上不同于我国文革中的大辩论，它尊重程序和规则，不论是什么不同的意见，任何一方都有同样的话语权，有相同的发言时间，不能以政治权威压人，不能打断别人的发言，更不能进行人身攻击，胜负不在谁拳头大、嗓门高，能压服对手，或者用骂街的语言让对手落荒而逃，而在于谁能打动听众。在针锋相对的辩论场上每一位辩手面带微笑，不仅为对方精彩的陈词鼓掌而且言必称“对方辩友”和“请问”，礼貌坦诚的面对质疑挑战，最让人难忘的是，比赛结束时，失利的辩手常会主动上前，真诚祝贺对手的胜利。这种尊重对手、尊重异见的风度就算作为一种姿态也足以打动我。而这也正是我们的文化中历来缺少和漠视的。<br />
　　<br />
　　至今经过文革浩劫的人还常会把大学生辩论会和文革的大辩论联系到一起而产生恐惧的联想。其实文革的大辩论不是错在讲道理而恰是蛮不讲理。而当有一天真正“摆事实，讲道理”成为我们的社会解决不同观点和矛盾的途径，这才将是一个真正民主和文明的国家。</strong><br />
　　<br />
　　有人说，一个国家是否民主只要看它对待辩论的态度就可以了，一个国家的人民是否接受过民主意识的浸染熏陶只要看他们在辩论时的风度就可以了。从这个角度讲，大学生辩论赛对于塑造将要投身社会实践的青年群体独立思考和宽容精神的意义非小。<br />
　　<br />
　　在西方文明的滥觞之地希腊，传说当时智者派哲学家的代表人物普罗泰哥拉和伟大的政治家伯里克利两位老兄居然花了一整天的时间来辩论：<strong>究竟是标枪，还是扔标枪的运动员，还是主持竞技会的人应该对不幸被标枪刺死的人负责。还有苏格拉底，他的弟子柏拉图的三十余篇脍炙人口的对话记录了这位自诩为“雅典的牛虻” 的哲学家整天蓬头赤足与人争辩的业绩。恰是这种对讲道理的执著甚至吃饱了撑着的痴狂，哺育了追求真知的科学精神和宽容平等的民主意识。</strong><br />
　　<br />
