													<h2>【转】读《塔什干屠城》 亦歌</h2>
						
						
																											<div id="postmessage_301862" class="t_msgfont">读《塔什干屠城》 亦歌<br />
<br />
柏扬先生向来以揭露国人劣根性为己任，所著文章往往及其犀利尖刻，这本也无所非议；然倘若一味剑走偏锋，并不惜为此断章取义，歪曲史实甚至道听途说，那就不免要弄巧成拙了；所著《塔什干屠城》便是一例。<br />
<br />
按照柏扬的说法，“石国，一个富庶、和平、友善的国家，将近两千年之久没有战争。”……石国人民只是从祖宗流传下来的传奇故事中，听说过在东方有“这么一个万人尊敬的礼义之邦，仁爱、忠孝、信义、爱好和平，闻名于世，是塔什干城人民响往的流奶与蜜之地。”<br />
<br />
事实上，石国在唐朝是昭武九姓之一，“石”便是其姓。早在显庆三年（公元６５８年），唐朝就在石国设了大宛都督府，其后石国一直向唐朝进贡不断，公元７４１年，石国王还上贡表请求唐朝发兵联合突厥共同抵抗来自大食（波斯）的威胁。天宝五年（公元７４６年），石国又进贡良马五十匹，朝廷封石国王子为怀化王并赐铁券，公元７４９年八月，也就是所谓“塔什干屠城”的前一年，石国王子远恩亲自来贡。石国和大唐有着这么千丝万缕的联系，柏扬怎么能说石国只是“从祖宗流传下来的传奇故事中”听说这个东方古国的？<br />
<br />
却说对这么一个刚在一年前还来朝贡过的友好附属国，莫非是玄宗脑筋不正常了还是高仙芝吃了豹子胆了，敢随随便便就给灭了？要回答这个问题，还是王国维老先生的一段话简练：<br />
<br />
“西域人民，以国居东西之冲，数被侵略，亦遂专心职业，不复措意政治之事，是故希腊来则臣希腊，大夏月氏来则臣大夏月氏，…九姓昭武来则臣九姓昭武，突厥来则臣突厥，大夏来则臣大夏。……”<br />
<br />
石国在改宗昭武九姓以前臣西突厥，甚至连石国王都是突厥人。七世纪中叶，唐朝在该地区的势力渐渐强盛，石国便有了投靠大唐之心。公元（６５７年），西突厥部首领阿史那贺鲁在和唐军交战中失利，带着一小部分人马流窜到石国西部的一个小城，该城城主用酒食把他骗进城后拿下，后来把他作为讨好唐军的礼物送给了大唐。可以说是卖主求荣。<br />
<br />
大约公元（７０８年）起，由于大食的势力在该地区的不断扩大，该地区的突骑施部落带头叛乱，进犯大唐边境，并杀死了大唐使者。两年后，中宗决意北伐，组织了八十万大军准备开拔，不料中宗被韦后毒死，北伐流产，朝中主和派势力占了上风。此后的几十年里，这些部落或叛或降，朝秦暮楚，到了公元７４７年，也就是“塔什干屠城”前四年，这一地区有二十几个小国全部依附了吐蕃，并联合身后的大食和大唐为敌，彻底堵塞了大唐去西域的通道，唐玄宗派高仙芝领兵万骑出征，灭了小勃律（今克什米尔的吉尔吉特）使得吐蕃无法和大食联手。这在唐代历史上具有极其重要的意义。不料第二年，吐蕃大举反攻，唐军战败，玄宗不得已又派高仙芝领兵出征，征服了突骑施，吐蕃，石国和其余几个小国，将这些酋长可汗们通通献俘长安。两年后，哥舒翰又趁机收复了九曲故地，大唐才在河西和陇右取得决定性的胜利。所以，高仙芝入石国有其一定的历史原因，按照《唐会要》卷九十九〖石国〗所载：<br />
<br />
……天宝“九载，安西节度使高仙芝奏，其王蕃礼有亏，请讨之，其王约降，仙芝使部送，去开远门数十里，负约，以王为俘，献于阙下，斩之，自后西域皆怨，仙芝所擒王之子，西走大食，引起兵至恒罗斯城，仙芝军大为所败，自是西附于大食，至宝历二年及大历七年，并谴使朝贡。”<br />
<br />
其中“蕃礼有亏”指的就是石国有叛心，高仙芝前去征讨，石国王是深知其因的，因为无法以卵击石，所以才申明投降，并想用淳酒和美女来通融一下，而非如柏扬所说的那样“举行一次嘉年华会”以欢迎大唐的友谊使者。至于高仙芝“负约”，恐怕是有因在先：当年打小勃律时，该国官民通通逃入山谷，高仙芝知道如抓不到元凶，等大军一退，该国复叛如故，于是就动了一个脑筋，派遣人去骗说大唐天子要赏赐东西，让这些人赶快前来领取。结果，等众人到以后，高仙芝立刻逮捕并处死了许多依附吐蕃的大臣，只因勃律王和吐蕃公主老奸巨滑，才没有落入罗网。四年后，高仙芝在石国又是如法炮制，并如愿拿下包括国王在内数名为首的。数月后，随同石国国王押解长安的还有突施可汗，吐蕃酋长等，高仙芝为此封了开府仪同三司。<br />
<br />
其实那次远征的影响极其深远，石国国王被解往长安后，他的儿子跑到大食去引了一支兵来打唐军，由于唐军中也有许多归依大唐的少数民族部落的军队，其中有一支突厥兵突然叛乱，在大食和叛军的两面夹击下，有数千人被俘虏，高仙芝大败而归。这些俘虏中有不少能工巧匠，到了大食后，极大地发展了大食的手工业，也建起了第一家西亚的造纸厂，中国的造纸术也正是如此才传入欧洲，此是后话不提。<br />
<br />
除上所述，《塔》文中尚有不少其它错误，如高仙芝在石国要和其国王缔结盟国关系，其实那时石国的国王受唐朝册封早已快一百年了，高仙芝怎么能自说自话地和一个附属国结盟；再如柏扬说在舞会上“高仙芝将军拥抱柔若无骨的玛琳娜皇后起舞……”，想象力真够可以的；大唐粗鲁的武将都能搂着女人大跳交谊舞了！事实上，九姓胡的贡表上常有“胡旋女子”一栏，倘若让高仙芝高高在上喝着葡萄美酒，观赏石国皇后令人眼花缭乱的“胡旋”，那倒是令人信服的。又如说高仙芝谋反一事，与史实不符；高仙芝被斩首主要是因为边令诚诬告高仙芝“丢师弃地，偷减军士粮赐”，玄宗大怒，下令斩首，而不是柏阳说的是因为谋反。另外，说皇后的面前形成了一个诅咒的磁场，弓箭无法穿透及宝塔从中裂开云云，更象是用来弹唱的民间故事，而不是严肃的史料文章所该引用的。<br />
<br />
有关西域的史料极其繁杂，记得一位作家曾说过：“历史就象是个婊子，谁有权有势都可以摸她一下。”这么多人摸下来，自然各有不同的摸法，要看怎么舍取，而柏杨先生这般摸法，似有断章取义，哗众取宠之嫌，要这样来针砭国人，恐怕还不如拎桶浆糊，到大街上多刷几幅“丑陋的中国人”的大标语来得更直截了当些。</div>

							
							
							
							
