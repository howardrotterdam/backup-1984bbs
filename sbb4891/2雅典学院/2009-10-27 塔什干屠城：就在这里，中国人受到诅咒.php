<br />
“逮捕国王，不要杀他，我要带他到长安献俘。把皇后送到大营，如果她有一根头发受到伤害，你们就死。”<br />
<br />
惊恐战悚和悲痛眼泪齐涌的玛琳娜皇后，在士兵挟持下，登上软轿，她为暂时保住性命的丈夫松一口气，但是当她穿过刚才还是载歌载舞的街道时，却目睹血腥扑鼻，满地都是儿童的尸体，或奋起格斗而被杀戮的塔什干人残骸，以及将死的人的凄厉呻吟。远处，就是被集中在一起的全城妇女，她们还穿着嘉年华会多采多姿的舞衣，现在却陷于绝望的哀声呼号。玛琳娜皇后收起眼泪，她发现除了接受恶魔高仙芝将军的蹂躏外，她已没有第二个选择。而她的臣民，以及塔什干城，却再也不能复活！突然，一星点儿时的记忆在心底浮出，她来自巴尔喀什湖之北的一个名叫曼克隆的奇异部落，是个以恩怨分明著称的正义之族，祖传一种誓不宽恕的诅咒。玛琳娜皇后善良宽厚的心，从小就反对复仇，因为她不相信人与人之间有非复不可的仇。<br />
<br />
伤心的玛琳娜皇后经过阿洛依寺，那是曼克隆部落为他们出嫁的女儿玛琳娜所建的善恶之神的庙宇，一座印度式的七层宝塔――阿洛依塔，矗立在后院，玛琳娜皇后注视着它，然后，庄严的吩咐卫士：“我要向神灵祈福！”没有人能反抗美丽的皇后的要求，即令她已失去了权势。于是，玛琳娜皇后跨下软轿，奔向阿洛依塔，像闪电一样，她闯进塔门，迅速转身把塔门从内部关闭。<br />
<br />
一匹快马奔向皇宫，高仙芝将军正坐金銮宝殿上清点抢劫到手的金银财宝。<br />
<br />
“报告大帅，我们阻拦不住，皇后逃进高塔！”<br />
<br />
高仙芝将军从座位上跳起来，一支短矛随手掷出，恰恰透穿那个报信的骑兵卫士的前胸，作为他第一个透露恶耗的回报。然后高仙芝将军上马出宫，当他站在阿洛依塔下时，玛琳娜皇后已在塔顶小门出现，高空气流拂动她宽大的衣袖和裙摆，正像一尊女神雕像。<br />
<br />
“皇后陛下，”高仙芝将军唤，“请你下来，我保证答应你的要求。”<br />
<br />
没有回答，只有一声长啸，发自塔顶的那个女神雕像口中，像镰刀一样，刈平世间所有的声音，只除了她要说的话。她伸展双臂，仰头向天，呼喊：“阿洛依神，听我诅咒！”<br />
<br />
“陛下！”高仙芝将军忽然间惊觉到他正面对另一个世界，他那被狂傲和贪欲搅昏了的头脑，稍稍平静，他想起所听说过关于曼克隆族的传说。<br />
<br />
“皇后是哪裹人？”他问。<br />
<br />
副司令官封常清回答：“曼克隆部落！”<br />
<br />
“果然，”高仙芝将军下令，“射死她，射死她的有重赏！”<br />
<br />
箭如雨下，但都在玛琳娜皇后面前落下，她四周已经形成一个诅咒磁力场。<br />
<br />
“我诅咒，”玛琳娜皇后呼喊：<br />
<br />
“我诅咒：塔什干城的血一定要中国人偿还，塔什干人受的屈辱、劫难，将原封不动加到中国人身上。我诅咒你们行为粗野、心灵败坏，你们最大的喜悦就是看到自己中国人身遭不幸。你们世代相传的德行徽章，将被践踏在地，用脚踩碎。<br />
<br />
“我诅咒：诅咒中国从一个充满活力的国度，化成一个庞大的泥泞沼泽，中国人就深陷这个泥沼之中，扭曲、变形、神志不清。我诅咒你们中国人将一直缅怀过去光荣，但你们被邻国尊重的盛世，从此绝迹。我，曼克隆族的女儿，诅咒你们中国人失神落魄、目光呆滞，会被冷箭射死。温暖的井枯竭，和照的风停止，而你们得不到救赎。<br />
<br />
“我诅咒；诅咒你们中国人永不停息的互相杀戮，左手是右手的仇人，右手要砍断左手，你们的怨毒会蔓延到你们民族发源地母河，使它黄沙滚滚，不断的泛滥，但你们的怨毒只会对自己人，在外国人的面前，你们不过一只可怜的虫豸。<br />
<br />
“我诅咒：善恶分明的阿洛依神啊，垂听，你要使中国人是非不分，黑白颠倒，犹如白昼和黑夜相反。中国人听不懂什么是天上韶音，而只知道咆哮。我诅咒，你们中国男子无福消受女子的温柔，你们新生的婴儿会被烙上死亡烙印，自家门墙将洒上自己婴儿的血。<br />
<br />
“我诅咒，诅咒你们中国人的荣华富贵，像沙漠中的水珠，转眼蒸发；中国人的丰功伟绩，只是一堆沙碛，很快地被自己的脚踏平。阿洛依神啊，你要使苦难紧紧抓住中国人，使他们每走几步都要跌倒，站起来再走，再走几步再次跌倒，永远如此。任由战争、饥馑、天灾、昏庸、贪污、怯懦宰割，你们不懂得呻吟，也不敢呻吟。中国人找不到自己的灵魂，像在湍急的溪流，照不见自己的影像。”<br />
<br />
高仙芝将军脸色苍白，他紧急集合强弩，那是一种机械性发射的巨弓，可以一次发射十箭，一百架强弩瞄准娇弱的玛琳娜皇后，可是巨箭一进入诅咒磁力场，箭杆就发出火星，而且化成万条火炬，像彗星的尾巴一样，在阿洛依塔两旁，升入天空，消失得无影无踪。于是高仙芝将军想到了火，霎时间他知道他应该怎么办，下令聚集高达两层楼的木柴，纵火烧塔。<br />
<br />
“玛琳娜皇后，”高仙芝将军叫，“你已经疯了，你么做不到。”<br />
<br />
“我当然做得到，我们的阿洛依神，以眼还眼，以牙还牙！”<br />
<br />
高仙芝将军下令在烈火上烧油。<br />
<br />
“中国人的好运已经结束，”玛琳娜皇后说，“仍活在世上的中国娘亲，将永远哀哭你们儿子的丧生！”<br />
<br />
“我不久就西征恒罗斯国，我的大军将消灭黑衣大食，中国西疆将扩张到地中海，没有人能抵挡得住，你的戏法白费唇舌！”<br />
<br />
转瞬间窜升的火焰冲破黑暗的天空，像血红巨舌一样，舐到宝塔的顶层屋檐，玛琳娜皇后已被火舌锁住，她知道已经没有时间。<br />
<br />
“我诅咒：中国西方边界永远不能越过葱岭，你的大军将在恒罗斯城下惨败，在塔什干城杀人的凶手，将在恒罗斯城下，用血偿还。”<br />
<br />
一声巨响，阿洛依塔从中间裂开，而玛琳娜皇后仍在厉声高叫：<br />
<br />
“我诅咒：你这个豺狼，高仙芝将军，你将被你效忠的政府斩首，犹如我们被敬爱信任的朋友出卖！”<br />
<br />
阿洛依塔崩塌，玛琳娜皇后葬身火窟，中国远征兵团士卒呆在那裹，手足失措，鸦雀无声，玛琳娜皇后的诅咒每个人都听得清楚，都认为他们遇到了厄运，这是不祥之兆！高仙芝将军面无表情，沉默的走回大营，他下令全军不准传播，违令的处死。<br />
<br />
史书记载，塔什干屠城的第二年（七五一年），高仙芝将军率中国远征兵团及外籍兵团，西进七百里，抵达恒罗斯城；外籍兵团叛变，跟黑衣大食援军，发动夹击，中国远征兵团大败，只剩下数千人，包括高仙芝将军在内，向东方撤退。<br />
<br />
当他们撤退到塔什干城时，恐怖抓住残兵败将，恒罗斯城溃败的诅咒应验了，高仙芝将军在已成一堆焦土的阿洛依塔前站定，摆上香火祭品，高仙芝将军跪下，流下哀求的眼泪，他举起香火，询问：<br />
<br />
“皇后陛下，你加到中国人身上的诅咒，什么时侯可以解除？”<br />
<br />
没有回答，高仙芝将军再度焚香，香烟袅袅上升，升到大约当被塔顶的高度，士卒们听到一种似乎来自天际的音乐，仿佛是玛琳娜皇后的声音，那声音跟当初大不一样，十分柔和安宁：<br />
<br />
“它永远不会解除，除非中国人醒悟到自己受到诅咒。”<br />
<br />
玛琳娜皇后的声音消失，香烟迅速的四散。一群土狼环绕着城垣哀嚎，这种只有夜晚才出动觅取腐尸狂嚎的野兽，每一声哀嚎都使远征兵团残余士卒打一个冷颤。<br />
<br />
玛琳娜皇后诅咒后的第五年，七五五，范阳军区总司令官（范阳节度使）安禄山将军叛变，攻击潼关；高仙芝将军奉命前往阻截，宦官边令诚诬陷高仙芝将军谋反，高仙芝将军遂被斩首。<br />
<br />
追随高仙芝将军的远征兵团士卒，早已死尽，玛琳娜皇后的诅咒也再没有人记得，更没有人提及。这场历史公案，就这样淹没。<br />
<br />
直到今天。</div>

							
							
							
							
