<br />
不知道多少万年之前，中国人的始祖在黄河流域逐渐定居下来，他们像太阳的辐射线一样，发出无限光芒。历史上从来没有像汉民族这样一个血统文化单一的民族，能够维持这么长久的活力，一直到纪元第八世纪。<br />
<br />
第八世纪，是中国人的转折点，唐王朝政府正以无比雄厚的威力，派出庞大兵团，向首都长安西北，航空距离三千五百公里外的中亚细进发，第一个目标就是塔什干城。<br />
<br />
塔什干城，史书上称为“石国”，一个富庶、和平、友善的国家，将近两千年之久没有战争。当他们的国王尤金斯听到中国友谊的手正向他们伸出时，他充满了兴奋。因为，祖宗流传下来的传奇故事中，曾经说过：遥远东方有个中国，是一个万人尊敬的礼义之邦，仁爱、忠孝、信义、爱好和平，闻名于世，是塔什干城人民向往的流奶与蜜之地。尤金斯国王命全国休假三天，作一次彻底清扫，然后分别制造花车，酿造美酒，他们将为世界上最文明国家的武装部队，举行一次嘉年华会，和一次通宵达旦的狂欢舞会，表示由衷的尊敬。全城最美丽的女郎将在舞会上出现，玛琳娜皇后将作中国友谊之军总司令官高仙芝将军的舞伴。高仙芝将军的英勇事迹流传国际，塔什干城人民都以能瞻仰这位盖世英雄为荣。<br />
<br />
这件事情，发生在七五○年。<br />
<br />
斥候官不断的报告中国友谊之军逐渐接近的消息，塔什干城的欢乐气氛，已如火如荼，鼓声和号角，交杂着唢呐，和中亚细亚特有的胡笳，声满四野。然而，没有人知道一个空前的大悲剧就要发生。<br />
<br />
高仙芝将军接受的训令是：一直向西挺进，重开七百年前的丝路，把中国国土，以及中国儒家学派圣人的教化，推展到地中海，但是，高芝仙将军庄严的面具下，想到的却是塔什干城的财富和美女。一份呈送到他桌上的资料，报告他说：“元帅，你如果得到塔什干城的财富和美女，就等于征服全世界，你荣耀的大旗，将从塔什干城一路插到你的故乡朝鲜半岛。<br />
<br />
所以，当高仙芝将军接受尤金斯国王使节晋见，发现尤金斯国王的虔敬心意时，十分吃惊，如果高仙芝将军有人性的话，他会为自己的卑鄙念头，感到惭愧，但高仙芝将军利欲熏心，因之他的反应却是暗中嗤笑：塔什干城君臣人民，竟是一群白痴。<br />
<br />
中国远征兵团士卒在游行花车两侧夹队进城，驰入预先准备好的营房，并且立即参与欢宴。高仙芝将军则被迎接到金銮宝殿，晋见尤金斯国王和玛琳娜皇后。在中国皇后永远封闭在深宫之中，从来不跟世人见面，玛琳娜皇后的出现对中国人来说，是一种震撼，但震撼力最大的还是她高贵气质和绝世美丽。高仙芝将军眼睛忽然一亮，如同看到一颗明珠，以至在整个宴会上他贪婪的眼睛一直盯住她，在她身上燃烧欲火。<br />
<br />
“大帅，”他的副司令官封常清提醒他，“你应该跟国王谈话，一个来自上国的将军不应该只看皇后。”<br />
<br />
高仙芝将军回答说：“我的军队可以弥补我的失礼。”<br />
<br />
但是，高仙芝将军仍然站起来向尤金斯国王举杯致词：保证中塔两国友谊万古常青，保证中国誓死遵守盟约――假如尤金斯国王愿跟中国结盟的话，中国将永远尊重尤金斯国主权独立，永不干涉塔什干城的内政，直到海枯石烂。尤金斯国王显然忽略了高仙芝将军对玛琳娜皇后的表情，因而诚心诚意的跟中国签订盟约，他跟高仙芝将军同时把牲畜的血抹到嘴唇上，全城人民为万世和平欢呼祝贺。<br />
<br />
傍晚，狂欢舞会在一片欢呼声中进行，高仙芝将军拥抱柔若无骨的玛琳娜皇后起舞，一种欲望在高仙芝将军血液里沸腾，他几乎不能等待，但碍于将军的威仪，所以他计划把美丽的皇后留到深夜；除了美丽的皇后，他耳旁还不断听到叮当作响的金币声音；而恒罗斯城――二十世纪的奥立阿塔，就在更远的西方向他招手。根据间谍的报告说：恒罗斯城已向黑衣大食帝国求救，希望高仙芝将军迅速赶往，要在大食军抵达之前，夺取那个当时西方最大的城池。一种万事顺畅，胜利在握的骄狂，化做踌躇志盈，对自己十分满意的笑容，然后，刹那间，高仙芝将军下令结束舞会，但他不准玛琳娜皇后离开，他紧握着玛琳娜皇后的手，一面向尤金斯国王致谢他的招待，一面提出要求：<br />
<br />
“陛下，”高仙芝将军微微一鞠躬，“为了贯彻打击恶魔行动的全盘胜利，和中塔两国永恒友谊，从现在开始，皇后暂时归我保护，贵国所有女子也暂时归我的部属保护，男人编入我们的战斗序列；因为我们绝不充许感情的包袱妨害我们伟大神圣事业的完成，所以对你们所有的儿童，我们将作更妥善的安排，好把绊脚石化成前进的动力。等到胜利之后，我保证，我们会第一优先考虑使塔什干复国。谢谢你的合作！”<br />
<br />
纵是一个霹雳打到脚下，也不会使尤金斯国王如此震惊，他手中的酒杯掉到地上，发出清脆的响声，敲碎了天真善良的心，但也敲出一声丧钟，尤金斯国王还没有来得及申诉，高仙芝将军已经认定跌碎酒杯显然是发出反抗的信号。他说：<br />
<br />
“我遗憾你使用暴力，遗憾你破坏刚刚缔结、墨迹未干的盟约，你会看到你吞下承诺的后果。”<br />
<br />
于是，高仙芝将军下令屠城。<br />
<br />
