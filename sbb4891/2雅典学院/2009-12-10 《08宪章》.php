刘晓波博士身陷囹圄已经整整一年了。<br />
<br />
Photo: RFA<br />
一年前的今天下午，晓波在Skype上呼我，然后告诉我，他家楼下警察上岗了，问我这儿是不是也那样。我说，至少到中午为止，我这儿没见到人。他问：你看他们这次上岗为了什么？我说，应当是冲着后天“世界人权日”而来。他说：我也这么想。不过，他们爱咋的咋的吧。<br />
2008这一年，警察上岗的事实在太多了。所以当时我和晓波都没把“上岗”和“抓人”马上链接到一块。事后刘霞告诉我，她早几天就明确地向晓波发出了“你将很快被抓！”的预警信号。而晓波又是如何应对她的警告的呢？刘霞说，晓波将《零八宪章》的最后定稿文本提前发到国外，然后还是，该干嘛干嘛。<br />
这就是晓波。面对日益逼近的牢狱之灾，他不躲，也不逃。去年11月，他和祖桦等人为《零八宪章》征集签名和敲定文本一事进入最后倒计时。记得其间晓波曾两次对我说：他会被抓，最晚是在明年六四20周年纪念日之前。面对他清醒而略显沉重的坦陈，我不可能违心地说别的什么话，只能如实地、狠下心默默地点了点头。他两次对我说，我点了两次头。事情明摆在那里，晓波带头挑战中国的言禁，挑战《刑法》第105条第二款这一恶法，他所面临的，就是坐牢的风险。我心里很清楚，晓波会象波兰的米奇尼克和捷克的哈维尔一样：这种牢，该坐的时候就得坐。这种坐牢，不是被人抬晕后的一时冲动，不是受人忽悠后的“无畏壮举”，而是他冷静的理性选择。<br />
一年前的今天深夜，晓波被蜂拥而入的冷面警察从家中、从刘霞的身边强行带走，消失在浓浓的黑幕之中。我完全可以想见，从离我家仅一街之隔的七贤村昂首而去的刘晓波博士，将以他忍受苦难的能力，去较量当局制造苦难的能力；将用他灵魂的力量，去抵御当局物质的暴力。<br />
就在晓波与我们分手第二天，凝聚着他和其他人心智与共识的《零八宪章》问世了。这份闪耀着真理和“常识”光芒的中国人的宪章，既和捷克的七七宪章一样，是一份鼓舞人心的人权宣言；同时，还是一份回应时代需求的、负责任的政治纲领。可以毫不夸张地说，这份历史性文献使60年来的所有官方文本黯然失色，甚至无地自容。足以让晓波感到欣慰的是，在303位首批联署人之后，一批又一批中国民众，以真名实姓签署了宪章，其中不少人还留下了十分感人的肺腑之言。《零八宪章》联署人，真配称得上是后极权制度下尊严地站出来的公民群体。签上真名，是心中的尊严正在觉醒的外在标志；签上真名，表明算数，体现担当，具有公信力和道德感召力。一位签署人留言道：只有坚持并落实《零八宪章》的理念，才有做一个人的基本尊严。我愿意签名，并承担与此相关的一切后果。这份短短的留言所彰显的，正是当下中国亟为必需的公民精神，是晓波在自己的言行中常常展示的公民精神。<br />
2001年9月10日，我在北京市第二监狱中曾对《国际歌》歌词作了改写，把它改成了《公民歌》。《公民歌》的第三段歌词是：<br />
是谁刷新了人类世界？是宝贵的公民精神。一切凭良心所驱动，那能容得柏林墙。最可恨那些冒牌真理，蒙蔽了我们的心灵，一旦把它们扫荡干净，人性的光芒照遍全球。<br />
这是最后的斗争，团结起来到明天，宪政民主法治就一定要实现。<br />
一年来，《零八宪章》签署人的公民精神经受住了各种打压的考验。为数众多的签署人被警方约谈或找去“喝茶”、传唤甚至抄家，但是，没有出现一位签署人发表退签声明的事！或许有人会把违心地认错、认罪看成是一件轻飘飘的事。但我不这么看。许许多多《零八宪章》的签署人也不这么看。签名是郑重的表达，也是庄严的承担。这种承担理所当然地首先包括：在打压下，不改变自己经风险权衡后自主表达出来的意愿。一年来的事实表明，《零八宪章》的发布在中华文明史上的确写下了崭新的一页，而一个个鲜活的《零八宪章》联署人，则正在中国大地上书写着自己的公民史。<br />
在本文写作至此的时候，刘霞发来了短消息：晓波案已被移送检察院。这时是下午5点37分。显然，当局偏与普世价值对着干的以言治罪之心是既定的，而各种压力也还没有达到能迫使当局收手的临界点。不过，同样显然的是，晓波捍卫人权、承受苦难之志也是既定的：三军可以夺帅，公民之志不可夺也。<br />
现在，晓波正遭受后极权制度的迫害而在大墙之中受难。以我对晓波的了解，我认为，有两件事是晓波最希望我们去做的，一是继续以宝贵的公民精神，大力传播《零八宪章》和征集签名；二是更有成效地推展以《零八宪章》为旗帜的中国公民运动。以《零八宪章》为我们共同灵魂的联署人，让我们用做好这两件事来表明自己的公民之志，表明我们和刘晓波的不可分割。<br />
