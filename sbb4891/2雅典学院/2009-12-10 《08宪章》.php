													<h2>零八宪章，是中国软实力的基本典范</h2>
						
						
																											<div id="postmessage_365664" class="t_msgfont">零八宪章：中国人普遍性身份的宣言<br />
陈奎德<br />
<br />
主席、各位来宾：<br />
<br />
今天站在这里，出席民主教育基金会对零八宪章群体的颁奖仪式，我深感荣幸。这是历史性的一刻。我回忆起一年多以前，当宪章面世之前近两个月，刘晓波先生传来宪章文稿征求意见，阅读之下，不仅想起了七七宪章，同时还唤起中国一百多年前“公车上书”的鲜活想象，想起了发生在今天开会的旧金山孙中山纪念馆周围的百年风云，甚至还追溯到更远的中国道统，从中听到东汉时期太学生学潮的潮声、明代东林及复社的士大夫的以道统学统抗衡政统的遗训，以及二十年前天安门广场《首都各界爱国维宪社会协商联席会议》的未遂之愿。接读文本，我已意识到这将是一份载入历史的文献。但即使如此，我仍然未曾预料到它会激发如此重要的反响，仍然没有意识到它会引来上万名前赴后继不顾身家性命的签署者，而且，迄今为止，在重重威胁利诱下，择善固执，无一退出。这在中国，是破记录之举。<br />
<br />
1895年，康有为、梁启超等中国读书人，为反对签订《马关条约》，写成一万八千字的《上今上皇帝书》，提出“拒和、迁都、练兵、变法”等主张，获得内地十八省与中国东北、台湾举人共一千二百多人联署，5月2日，康、梁十八省举人与数千市民集结在「都察院」门前，请为代奏，此举惊天动地。虽然上书被清廷拒绝，但在社会上产生了深远影响。从此，维新思潮风起云涌，“变法图强”成为主流，开启了戊戌变法及其之后中国宪政努力的一系列变迁。<br />
<br />
今天在中国，从三百零三直到到一万人之众，在严酷铁幕下，把自己的真名签上零八宪章，实际上是中国经历百年曲折迂回之后，重续先贤薪火，再启变法革新。它象征着，在森严的暴政之下，出席一场公众集会，交托一份庄严承诺，投出一张神圣选票。但其重心，已不是奏请当局，而是诉诸民间；其签署者，也不再局限于知识精英，而是遍及各个阶层。<br />
<br />
如此，零八宪章，进入了历史。<br />
<br />
零八宪章的焦点，是普世性。<br />
<br />
宪章，凝结了我们的基本认同。它表明，中国人并非异类，并非自外于文明人类的特色族群。我们同国际社会其他国家的人一样，同样应享有人的尊严，享有言论、信仰、免于恐惧、免于匮乏的基本自由，同样应分享现代文明的核心价值和基本人权。<br />
<br />
中国人也是人，不是特殊物种。我们首先是人，是地球公民，然后才是中国人。把中国人排除于宪政民主之外，是对国人的侮辱，是对我们每个人权利的冒犯。在基本权利面前，不存在自外于普遍人性的所谓“中国特色”、“中国国情”。<br />
<br />
零八宪章，就是确立我们这一普遍性身份的现代宣言。<br />
<br />
所谓中国特色的“三个代表”，未经我们同意，无权代表我们。零八宪章，经由我们自愿签署，才是代表我们价值认同的文明符号。同时，正如有签署者所强调的，他之所以毅然签署，是而且只是认同文本，不管其他签署者或未签者是谁。这种“只认文本内容，不管人际脉络”的行为准则，超越了历来政治传统中那种“党同伐异”的无原则派系纠纷，彰显了现代政党政治的风范。在这一心态下的签署，就承载了现代文明真正的“重迭的共识”，界定了民主宪政的普世价值和判断标准。<br />
<br />
零八宪章，是中国软实力的基本典范。<br />
<br />
人们注意到，近年来，北京当局竭力向外推销自己的“软实力”，在“硬实力”支撑下，慷纳税人之慨，不惜以亿万民脂民膏，收购海外媒体以为喉舌，同时办起二百多所孔子学院， 来势汹汹，洗脑天下，企图重建合法性。<br />
<br />
不过，我可以在此断言，就是把这二百多所孔子学院加在一起，它们的软实力，也不敌薄薄的一纸零八宪章。这并非说宪章有多么高明，原因无他，仅仅由于它奠基于现代的基本常识上，它诉诸“人同此心，心同此理”，这就赋予它不可摧毁的道义力量及合法性。而北京全球开花的孔子学院，除了是对中国古代先贤的第二次侮辱外，还可能是什么呢？它提醒人们的，无非是当年他们的批林批孔闹剧罢了。<br />
<br />
只要深入思考一下软实力的原初涵义，我们当会发现，某事物的软实力，其实是可以由该事物被封锁的程度来界定的。被封锁愈森严，其软实力就愈强大。而零八宪章，正是由于其孕育了强大软实力，而被封锁禁闭的。谓予不信，那么，请解放零八宪章，让它与北京出钱的形形色色喉舌们以及孔子学院们，在话语市场上公开公平地竞争吧。在这里，当局的对开放宪章的恐惧将映证我对软实力强弱之势的判断。如若不服，请“推到这堵墙”，解禁宪章，公开竞争！<br />
<br />
我想他们不可能有此胆识。其怯懦竟到如此地步，连拘禁和逮捕刘晓波先生的真正原因是《零八宪章》竟也不敢公开承认；面对全球滔滔舆论，只能以晓波近年来公开发表的文章入罪。而能发表这些文章，与当局当年与民间异议人士之间的某种默契有关，而且已持续多年。这次他们竟公然出尔反尔，打破默契，冒天下之大不韪，直接诉诸暴力。这就表明，所有的手段已经失败，图穷而匕首现，只剩下赤裸裸暴力镇压了。有鉴于此，与其愿望相反，这种逮捕所显示的，是其虚弱，而不是强大；是其失败，而不是胜利。是丧失软实力，而不是赢得软实力。<br />
<br />
零八宪章，是中国公民社会的粘合剂。<br />
<br />
李慎之先生曾判断，中国人迈进宪政民主的条件，在今天，“已经烂熟”。零八宪章正是在此背景下，应运而生。它把大家所拥有的“烂熟”的精神条件和诉求，凝结在五千言中。而这五千言，又将进一步发挥精神整合的功能，在构建中国公民社会的进程中，创造历史。<br />
<br />
在此，我必须提到刘晓波先生、张祖桦先生等各位执笔者和签署者为此付出的代价。我相信，晓波所遭受的磨难，将成为十字架，为宪章运动注入了沉甸甸的的道义力量，使之具有更广泛的价值认同和更深厚的道德感召力。<br />
<br />
零八宪章，是公民维权的基准法典。<br />
<br />
今天，人们日益体会到了，我们的权利，是高度相关的；倘若他人受难我们不伸出援手，那么，明日我们受难，也将孤立无援。我们将被切割开来，各个击破，普遍受害。因此，人溺我溺，守望相助，日渐成为共识。今天的中国民间，众目仰望的，不是管制我们的人，而是维护我们权利的人；不是官方典范雷锋王杰，而是民间英雄蒋彦永、胡佳、赵紫阳、鲍彤、高智晟、高耀洁、丁子霖、刘晓波。众人寻觅的，不是中央文件官方报刊，而是披露真相的博客网刊。一切社论官话，都成了笑料。一切谎言虚构，都成了废话。《零八宪章》的破土而出，以权利为根基，凝结了人们的基本愿景。这份万人签署的重迭共识，划定了我们权利的范围，是公民权利的守护者，是政治合法性的基准标杆，是一次公开地系统地界定我们政治身份的当代宣言，是中国加入现代文明世界的身份证。<br />
<br />
就共产国家转型的脉络看，从1917年共产主义在俄国得手，到1977年《七七宪章》出土，是六十年。与此平行，自1949 年中共建政，到今年零八宪章成势，也是六十年。虽然，为政治过程算命是无稽的；但时间的长度，增加了变化的可能性空间，的确孕育了历史演化涵义。明乎此，不难判断，中国社会今日的成熟度，与东欧当年的社会成熟度，有其重要的比较意义，有其基本的相似特征。基于这种成熟度演化的宏观历史比较，可以断言，在本世纪上半叶，中国转型为宪政民主将是不可抗拒的。<br />
<br />
从中国本身的历史脉络看，恰如刚刚过世的华裔历史学家唐德刚教授指出：“在西方文明挑战之下，我们的传统制度被迫作有史以来「第二次政治社会制度大转型」。……这次惊涛骇浪的大转型，笔者试名之曰「历史三峡」。我们要通过这个可怕的三峡，大致也要历时两百年。”按照唐先生的大体算法，中国穿越历史三峡之日，也将落实在本世纪上半叶。<br />
<br />
这当然不完全是巧合。可以预见，无论是与东欧苏俄的历史比较，还是观察中国现代的演变趋势，不管基于哪种脉络，我们都将看到零八宪章在中国大转型中所具有的枢纽地位。，它正在成为当代中国民间的一面旗帜。它是在适当的时间、适当的地点、以适当的姿态，发出的适当的声音。仅仅基于这一点，它也已经镌刻在中国历史上了。谢谢大家！</div>

							
							
							
							
