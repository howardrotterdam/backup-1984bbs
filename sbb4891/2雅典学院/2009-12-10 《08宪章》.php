三百多位公民联署，发起公布了“零八宪章”。 ”宪章“还没有上网，就有两位联署人被当局抄家，传讯和拘留。他们是刘晓波先生和张祖桦先生。我也是当事人，也是这三百多人中的一个。在认真准备和主动等待被抄家，被传讯，被拘留，被判刑的时刻，有几句不得不说的话。<br />
一.我要求当局回答：“零八宪章“有什么罪？ ”宪章“的基本理念是自由，人权，平等，共和，民主，宪政。请公仆让十三亿主人明白：自由何罪！人权何罪！平等何罪！共和何罪！民主何罪！宪政何罪！ <br />
<br />
“零八宪章”提出了十九条主张。这些主张，没有一条是我们这些人自己发明出来的，都是现代文明国家所已经实行了，并且已经起了好作用的好制度。请当局一条一条审查，拿出放大镜，拿出显微镜，拿出哈哈镜来审查。请问，这些主张，哪一条，犯了什么罪？ <br />
<br />
“宪章”的宗旨，是呼吁所有具有使命感的中国公民，不分朝野，不论身份，参与到公民运动中来，实现国人百年的追求与梦想。这算什么罪！ <br />
