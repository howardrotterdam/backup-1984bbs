													<h2>我所认识的游精佑</h2>
						
						
																											<div id="postmessage_213370" class="t_msgfont"><a href="http://my1510.cn/article.php?id=0d02c52382dfead2" target="_blank">http://my1510.cn/article.php?id=0d02c52382dfead2</a><br />
作者：马民博<br />
1990年7月，漳平铁路工务段来了一位大学生，人长得很清瘦，下额留着一小撮山羊胡子，脚上穿了双解放鞋却没有穿袜子，身上套了件工作服，远远看去像个仙风道骨的高人。<br />
<br />
这位大学生一天到晚往现场跑，为现场解决一些桥梁方面的技术问题，机关的一些干部对他的工作都是充分肯定的，但对他的衣着却颇有微词，认为不修边幅。那时候，咱作为一名专职从事思想政治工作的干部，对同代年青人是相当关注的，因此这位穿的衣服与其他机关干部并无多大的两样，效果却有天壤之别的大学生，就很快引起了我的注意。他为什么不修边幅？为什么要留山羊胡子？为什么不穿袜子？等等这些表象下到底隐藏着什么呢？想要反映什么？经过初步了解和接触，我知道他名叫游精佑，说话采用的是英语小说里倒装句的句式，毕业于西南交通大学。<br />
<br />
经过一段时间的交往，我们成了朋友，他请我们吃饭，还喜欢自己煮，怎么煮呢？他把买来的东西洗干净后倒入锅中一起煮，有点像做“佛跳墙”，味道可是远远比不上“佛跳墙”菜的，然后弄上一瓶绵竹大曲什么的酒喝上几杯。游精佑还是个爱看书的书生，他看的书与我不大一样，他以看外国书为主，比如《静静的顿河》什么的，还看英文版的书。我以看中国书为主，当然也看一些外国人写的书，但看的都是二手货DD译作，由于先天不足，英文版的书咱看不懂。他西方书籍看多了，自然受到西方文学的一些影响，受到西方生活方式的一些影响，致使他公民的社会责任感特别强烈，自由民主的理念特别强烈，人权法治的意识特别强烈，社会正义感特别强烈。有很多时候，一些弱势的人遇到困难了，可能许多国人都会事不关己高高挂起，而若被游精佑碰上了，他决不会袖手旁观，而是会伸出援助之手，也许他的援助也很微弱，但他会去做，他是一个行动者，而不是一个卖弄嘴上功夫的人。<br />
<br />
记得那是1992年6月底的一天，我因被蛇咬而住在漳平市某医院治疗。住院期间，可能由于我家人都在老家苏州的缘故，游精佑和我的那帮朋友每天都轮流来看望、照顾我。突然，有一天晚上的八九点钟，走廊里躺着两个人，满身是血，在地上呻吟着，非常痛苦。没人去理他们，游精佑刚好来医院看望我，发现这一情况后，他就去找医生问为什么不抢救，医生告知我们，他们交不起400元住院和手术的钱。游精佑去问其中一个伤势较轻的人，你们为什么会这样，从哪里来的。那个人告诉我们，他们从贵州到厦门去打工，结果在厦门没找到工作，钱也花完了，后来就爬上了北上的一列旅客列车，车快到漳平时，车上查票，因他们没有买票就跳下了火车，跳下火车后，他们从水沟里爬上了公路，拦了一辆三轮车，三辆车夫把他们送到医院。<br />
<br />
人摔成这个样子，其中一个胸部摔得很重，较轻的一个人是摔断了手臂。游精佑和我赶紧跑去对医生说：“救人要紧，住院的钱我们来想办法，我们来负责。”医生有了我们这句话，就立即对病人进行了抢救，其中有一个伤势非常重，医生后来告诉我们，那人要是当天不抢救可能就没命了。1992年，400多元钱对我们拿工资的人来说是一个大数字，那时我自己也住院，没有那么多的钱，怎么办？这要感谢游精佑他们，他们不但自己踊跃捐款，5、10元的凑了起来，还到各个病房去募捐，记得隔壁病房有一个年纪比较大的老婆婆，也伸出了援助之手，非常热诚，最后1、2元地募集到了400元钱给了医院。后来，医生和护士对病人也给予了相当大的关照。在抢救病人的同时，游精佑走路赶到邮电局帮忙发电报给他们贵州省的老家。游精佑等人还照看、护理两个受伤的贵州民工，为他们提供食物。后来，两个贵州民工老家赶来的一个在煤矿当干部的人，握着我的手说是非常感谢我，其实我想，他应该感谢的是游精佑他们。<br />
<br />
当我和玩在一起的朋友们还在充满理想仰望星空的时候，突然有一天一个朋友告诉我，游精佑杀进“围城”去了。古人说兵贵神速，游精佑是婚贵神速，他的婚礼除了单位里我们共同的一个朋友参加外，其余几个人都不知道而没有到场，保密工作做得非常好，婚礼在他老家长乐悄悄地完成了，过了年把时间还有了小孩。<br />
<br />
游精佑社会责任心强，对工作的责任心也是有过之而无不及。游精佑对技术问题很认真，为了弄清桥梁、路基的病害经常深入河谷爬上山脊。记得有一次我们陪同铁路局工务处的技术人员查看鹰厦线一处滑坡病害时，游精佑是跑到了山顶去查看病害的源头，检查得十分细致。由于工作出色，技术精湛，过了二三年，游精佑调到铁路分局工务分处工作了。人到了省城工作，再看到他时，山羊胡子不见了，衣着也整洁了，唯一还保留的是他的谦逊，由于谦虚他连走路时还有点弯腰，有时候也会听到有人说他重要的是缺少一种精神。现实生活就是如此奇妙，能无耻地说几句大话空话套话的人却被人说成是有精神，这就是社会。其实说他没有精神的人远没有了解游精佑的内心世界，虽然我与游精佑认识19年了，对他的了解也只是冰山一角。游精佑对技术问题态度非常严谨，他不会像某个铁路中专学校培养出来的人那样灵活、圆滑和投机，他执着于实际，注重于行动。后来，我还接触了一些西南交大毕业的学生，从他们的身上，发现西南交大培养出来的学生个性张扬，不修边幅，注重技术，不阿权贵，学校办到这个份上，我这个知识基本靠新华书店得来的人，也认为西南交大是所好学校。<br />
<br />
