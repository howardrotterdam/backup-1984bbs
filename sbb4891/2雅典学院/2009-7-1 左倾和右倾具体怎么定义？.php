一些当代的政治立场，例如在美国被称为“<a href="http://qq.ip138.com/w/index.php?title=%E8%87%AA%E7%94%B1%E6%84%8F%E5%BF%97%E4%B8%BB%E7%BE%A9&amp;variant=zh-cn" target="_blank">自由意志主义</a>”的立场，是很难以左派-右派称呼加以定义的。自由意志主义者在社会议题上属于“自由派”，但他们反对左派对于政府管制经济的支持，或反对右派对于贸易保护主义的支持。他们的政治立场可以说是最相似于所谓的<a href="http://qq.ip138.com/w/index.php?title=%E5%8F%A4%E5%85%B8%E8%87%AA%E7%94%B1%E4%B8%BB%E7%BE%A9&amp;variant=zh-cn" target="_blank">古典自由主义</a>―也就是1789年那时的左派。<br />
许多现代的思想家质疑左派-右派的区分法在21世纪是毫无意义的。当前大多数国家的左派-右派差异显然都是和当地的历史经验以及地区政策有关，而不是任何一致的原则理论。在<a href="http://qq.ip138.com/w/index.php?title=%E7%AC%AC%E4%BA%8C%E6%AC%A1%E4%B8%96%E7%95%8C%E5%A4%A7%E6%88%B0&amp;variant=zh-cn" target="_blank">第二次世界大战</a>之后，为了保持政治上的适当性，大多数西欧国家的右派都接受了大多数“左派”对于政府干预社会的观点。同样的，许多左派也在<a href="http://qq.ip138.com/w/index.php?title=%E9%9A%86%E7%B4%8D%C2%B7%E9%9B%B7%E6%A0%B9&amp;variant=zh-cn" target="_blank">雷根</a>-<a href="http://qq.ip138.com/w/index.php?title=%E7%8E%9B%E6%A0%BC%E5%88%A9%E7%89%B9%C2%B7%E6%92%92%E5%88%87%E5%B0%94&amp;variant=zh-cn" target="_blank">柴契尔</a>时期开始支持<a href="http://qq.ip138.com/w/index.php?title=%E7%A7%81%E6%9C%89%E5%8C%96&amp;variant=zh-cn" target="_blank">私有化</a>和<a href="http://qq.ip138.com/w/index.php?title=%E5%8F%8D%E5%85%B1%E4%B8%BB%E4%B9%89&amp;variant=zh-cn" target="_blank">反共主义</a>；而在最近，在后共产主义的中欧和东欧，即使是被归类为左派的政党似乎也支持限制国家在经济上扮演的角色。我们也可以见到一些运动如<a href="http://qq.ip138.com/w/index.php?title=%E7%B6%A0%E9%BB%A8&amp;variant=zh-cn" target="_blank">绿党</a>和<a href="http://qq.ip138.com/w/index.php?title=%E5%A5%B3%E6%80%A7%E4%B8%BB%E7%BE%A9&amp;variant=zh-cn" target="_blank">女性主义</a>的浮现，这些运动毫无疑问是更接近于传统的左派而非传统的右派，但通常是以他们对左派经济思想的排斥态度定义的。<br />
无论如何，政治的本质意味着两极的差异永远不会消失，至少地区性的政党通常会出于自身的利益考量而强调政策选项的两极―亦即非此即彼的左派-右派区分。将对手政党贴上左派或右派“激进份子”的标签也是经常出现的政治手段，也能用以划清或呈现出某人的立场、和某人所反对的为何。也因此，即使这左派-右派的称呼区别并不如以往一样有意义，但他们在可预见的未来依然会是相当重要的政治术语。左派-右派的区别肯定也会被一些自称舆论取向立场的团体所批评，例如<a href="http://qq.ip138.com/w/index.php?title=%E6%BF%80%E9%80%B2%E4%B8%AD%E7%AB%8B%E6%B4%BE&amp;action=edit&amp;redlink=1" target="_blank">激进中立派</a>、<a href="http://qq.ip138.com/w/index.php?title=%E7%AC%AC%E4%B8%89%E7%A7%8D%E9%81%93%E8%B7%AF&amp;variant=zh-cn" target="_blank">第三种道路</a>等等。这样的团体通常宣称自己“超越”了历史的区分界线，并坚持他们不属于左派也不属于右派。不过，他们还是经常被其他人划分为政治光谱的某一部分，无论他们反对与否。<br />
[<a href="http://qq.ip138.com/w/index.php?title=%E5%B7%A6%E6%B4%BE%E5%92%8C%E5%8F%B3%E6%B4%BE&amp;action=edit&amp;section=5" target="_blank">编辑</a>]关于“中国左右划分混乱”之说<br />
有部分人认为：中国左右的划分，跟西方差异很大，甚至刚好相反。<br />
例如：在西方，被称为“左派”的常常是那些重视保障公民权利、捍卫公民自由、关心弱势族群的人。<br />
而在中国，自称“左派”的人则是拥护中共“稳定压倒一切”，甚至不惜牺牲公民权利的人。<br />
而且，欧洲的一些左翼人士（社会民主主义者），对于前苏俄、中国大陆自称“左翼”的人士（亲共派）也并不买账。<br />
