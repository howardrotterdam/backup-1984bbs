													<h2>左派和右派</h2>
						
						
																											<div id="postmessage_154656" class="t_msgfont">词意<br />
尽管对这两词的使用相当普遍，然而这两词一般并没有准确的定义。也因此对这两词的使用往往是根基于舆论上。对于这两词的区分也有许多不同的看法：<br />
<strong>以“公民自由（个人自由）”为核心是左；以“经济自由”为核心是右</strong>：左派认为“公民自由（个人自由）”是核心，而“经济自由”只是经济模式的选项。右派则视“经济自由”为核心，认为“经济自由”会最终带来“公民自由（个人自由）”。<br />
<strong>公平的结果是左；公平的程序是右</strong>：<a href="http://qq.ip138.com/w/index.php?title=%E5%8F%A4%E5%85%B8%E8%87%AA%E7%94%B1%E4%B8%BB%E7%BE%A9&amp;variant=zh-cn" target="_blank">古典自由主义</a>强调程序的公平，<a href="http://qq.ip138.com/w/index.php?title=%E8%87%AA%E7%94%B1%E5%B8%82%E5%A0%B4&amp;variant=zh-cn" target="_blank">自由市场</a>便是例子之一。<a href="http://qq.ip138.com/w/index.php?title=%E8%87%AA%E7%94%B1%E6%84%8F%E5%BF%97%E4%B8%BB%E7%BE%A9&amp;variant=zh-cn" target="_blank">自由意志主义</a>学者<a href="http://qq.ip138.com/w/index.php?title=%E7%BE%85%E4%BC%AF%E7%89%B9%C2%B7%E8%AB%BE%E9%BD%8A%E5%85%8B&amp;variant=zh-cn" target="_blank">罗伯特・诺齐克</a>便是强调区分“历史过程”和“最后结果”两者的20世纪知名理论家之一<a href="http://qq.ip138.com/zt.htm#_note-0" target="_blank">[1]</a>。但另一方面，<a href="http://qq.ip138.com/w/index.php?title=%E7%8F%BE%E4%BB%A3%E8%87%AA%E7%94%B1%E4%B8%BB%E7%BE%A9&amp;variant=zh-cn" target="_blank">现代自由主义</a>的支持者如<a href="http://qq.ip138.com/w/index.php?title=%E7%BA%A6%E7%BF%B0%C2%B7%E7%BD%97%E5%B0%94%E6%96%AF&amp;variant=zh-cn" target="_blank">约翰・罗尔斯</a>则主张左派的政策也是使用以过程为根基的推论<a href="http://qq.ip138.com/zt.htm#_note-1" target="_blank">[2]</a>，而一些<a href="http://qq.ip138.com/w/index.php?title=%E6%96%B0%E4%BF%9D%E5%AE%88%E4%B8%BB%E7%BE%A9&amp;variant=zh-cn" target="_blank">新保守主义</a>者倾向使用军事手段建立<a href="http://qq.ip138.com/w/index.php?title=%E6%B0%91%E4%B8%BB&amp;variant=zh-cn" target="_blank">民主</a>才是属于最后结果。<br />
<strong>反对自由市场造成的<a href="http://qq.ip138.com/w/index.php?title=%E4%B8%8D%E7%AD%89&amp;variant=zh-cn" target="_blank">不均等</a>为左；而接受之则为右</strong>：通常，政治上的争论都是聚焦于政府究竟应该（<a href="http://qq.ip138.com/w/index.php?title=%E5%B9%B2%E6%B6%89%E4%B8%BB%E7%BE%A9&amp;action=edit&amp;redlink=1" target="_blank">干涉主义</a>）或不应该（<a href="http://qq.ip138.com/w/index.php?title=%E8%87%AA%E7%94%B1%E6%94%BE%E4%BB%BB&amp;variant=zh-cn" target="_blank">自由放任</a>）介入经济以救济贫穷的问题上。<a href="http://qq.ip138.com/w/index.php?title=%E8%AB%BE%E8%98%AD%E6%9B%B2%E7%B7%9A&amp;variant=zh-cn" target="_blank">诺兰曲线</a>将这个差异作为左派和右派两大差异的轴线之一。不过，政府的干涉并不一定就代表重新分配财富或<a href="http://qq.ip138.com/w/index.php?title=%E5%B9%B3%E7%AD%89%E4%B8%BB%E7%BE%A9&amp;variant=zh-cn" target="_blank">平等主义</a>的政策：一些形式的干涉是为了满足某些财团公司的利益，例如<a href="http://qq.ip138.com/w/index.php?title=%E7%A4%BE%E5%9C%98%E4%B8%BB%E7%BE%A9&amp;variant=zh-cn" target="_blank">社团主义</a>的政策。所以左派往往支持对于少数民族、中下层民众等弱势群体进行援助、保护，而右派偏好顺其自然、自由竞争，即使其结果是造成主导种族、阶层对政治和经济的掌控。<br />
<strong>偏好一个“更大的”政府为左；偏好一个“更小的”政府为右</strong>：政府的大小在这里可以视为是政策和立场的差异，虽然政府雇员的数量通常被用以作为主要指标。不过，一些人也注意到某些政治流派如<a href="http://qq.ip138.com/w/index.php?title=%E7%84%A1%E6%94%BF%E5%BA%9C%E5%85%B1%E7%94%A2%E4%B8%BB%E7%BE%A9&amp;variant=zh-cn" target="_blank">无政府共产主义</a>、<a href="http://qq.ip138.com/w/index.php?title=%E8%87%AA%E7%94%B1%E7%A4%BE%E6%9C%83%E4%B8%BB%E7%BE%A9&amp;action=edit&amp;redlink=1" target="_blank">自由社会主义</a>的存在与这种政治光谱不相合，反而如同<a href="http://qq.ip138.com/w/index.php?title=%E8%AB%BE%E8%98%AD%E6%9B%B2%E7%B7%9A&amp;variant=zh-cn" target="_blank">诺兰曲线</a>一般垂直于左右派之外。<br />
<strong><a href="http://qq.ip138.com/w/index.php?title=%E5%B9%B3%E7%AD%89&amp;variant=zh-cn" target="_blank">平等</a>为左；<a href="http://qq.ip138.com/w/index.php?title=%E8%87%AA%E7%94%B1&amp;variant=zh-cn" target="_blank">自由</a>为右</strong>：提出这种区隔的是哲学家<a href="http://qq.ip138.com/w/index.php?title=%E8%AB%BE%E8%B2%9D%E6%89%98%C2%B7%E6%B3%A2%E6%AF%94%E6%AD%90&amp;action=edit&amp;redlink=1" target="_blank">诺贝托・波比欧</a>（Norberto Bobbio）和Danielle Allen。波比欧主张唯一准确的左右派差异是有关人们对平等理念的态度，因为只有左派会想要保护或促进平等，而右派则会想要维持或增加不平等。左派和右派也同样都宣称同时追求平等和自由两者，然而他们对这两词却又有不同的解释方式。<br />
<strong>一个<a href="http://qq.ip138.com/w/index.php?title=%E6%94%BF%E6%95%99%E5%88%86%E7%A6%BB&amp;variant=zh-cn" target="_blank">现世</a>政府为左；一个<a href="http://qq.ip138.com/w/index.php?title=%E6%94%BF%E6%95%99%E5%90%88%E4%B8%80&amp;variant=zh-cn" target="_blank">宗教</a>政府为右</strong>：这种差异在美国、印度和欧洲的天主教国家特别明显（这些地方也是<a href="http://qq.ip138.com/w/index.php?title=%E5%8F%8D%E6%95%99%E6%AC%8A%E4%B8%BB%E7%BE%A9&amp;action=edit&amp;redlink=1" target="_blank">反教权主义</a>代表左派的区域），有时候也包含<a href="http://qq.ip138.com/w/index.php?title=%E4%B8%AD%E6%9D%B1&amp;variant=zh-cn" target="_blank">中东</a>。<br />
<strong><a href="http://qq.ip138.com/w/index.php?title=%E9%9B%86%E9%AB%94%E4%B8%BB%E7%BE%A9&amp;variant=zh-cn" target="_blank">集体主义</a>为左；<a href="http://qq.ip138.com/w/index.php?title=%E5%80%8B%E4%BA%BA%E4%B8%BB%E7%BE%A9&amp;variant=zh-cn" target="_blank">个人主义</a>为右</strong>：不过，1960年代的<a href="http://qq.ip138.com/w/index.php?title=%E5%8F%8D%E6%96%87%E5%8C%96&amp;action=edit&amp;redlink=1" target="_blank">反文化</a>浪潮便是以强调个人自由为特色，而这波浪潮主要则被归类为左派，而在宗教/现世的冲突上，现世主义者往往更倾向于强调个人的自由和<a href="http://qq.ip138.com/w/index.php?title=%E5%AE%97%E6%95%99%E8%87%AA%E7%94%B1&amp;variant=zh-cn" target="_blank">宗教自由</a>超越集体的信仰价值。不过，被许多人视为右派的<a href="http://qq.ip138.com/w/index.php?title=%E6%B3%95%E8%A5%BF%E6%96%AF%E4%B8%BB%E7%BE%A9&amp;variant=zh-cn" target="_blank">法西斯主义</a>也强调“国家的组织概念”，抱持着集体主义的概念，将国家看作是一个集体的实体。<br />
<strong>革新为左；<a href="http://qq.ip138.com/w/index.php?title=%E4%BF%9D%E5%AE%88%E4%B8%BB%E7%BE%A9&amp;variant=zh-cn" target="_blank">保守</a>为右</strong>：虽然在一些国家里“右派”和“保守派”往往被视为同义词，但这一区分在讨论左派右派的光谱时很少获得重视。<br />
<strong><a href="http://qq.ip138.com/w/index.php?title=%E6%B3%95%E5%BE%8B&amp;variant=zh-cn" target="_blank">法律</a>支配<a href="http://qq.ip138.com/w/index.php?title=%E6%96%87%E5%8C%96&amp;variant=zh-cn" target="_blank">文化</a>为左；文化支配法律为右</strong>：这个公式是由美国的参议员<a href="http://qq.ip138.com/w/index.php?title=%E4%B8%B9%E5%B0%BC%E7%88%BE%C2%B7%E5%B8%95%E7%89%B9%E9%87%8C%E5%85%8B%C2%B7%E8%8E%AB%E5%B0%BC%E6%BC%A2&amp;action=edit&amp;redlink=1" target="_blank">丹尼尔・帕特里克・莫尼汉</a>（Daniel Patrick Moynihan）提出才为人所知的，但最早则是由<a href="http://qq.ip138.com/w/index.php?title=%E5%9F%83%E5%BE%B7%E8%92%99%C2%B7%E4%BC%AF%E5%85%8B&amp;variant=zh-cn" target="_blank">埃德蒙・伯克</a>所构想的。<br />
<strong>支持跨国家团体为左；仅支持独立国家和政府为右</strong>：一些左派团体可能会被右派视为<a href="http://qq.ip138.com/w/index.php?title=%E6%81%90%E6%80%96%E4%BB%BD%E5%AD%90&amp;variant=zh-cn" target="_blank">恐怖份子</a>、但却可能被左派视为自由战士。右派的运动通常支持他们自己国家的<a href="http://qq.ip138.com/w/index.php?title=%E4%B8%BB%E6%AC%8A&amp;variant=zh-cn" target="_blank">主权</a>并反对其变动。在欧洲，支持<a href="http://qq.ip138.com/w/index.php?title=%E6%AD%90%E7%9B%9F&amp;variant=zh-cn" target="_blank">欧盟</a>者通常来自左派，而支持国家主权至上者则来自右派。<br />
<strong><a href="http://qq.ip138.com/w/index.php?title=%E5%9B%BD%E9%99%85%E4%B8%BB%E4%B9%89&amp;variant=zh-cn" target="_blank">国际主义</a>和<a href="http://qq.ip138.com/w/index.php?title=%E4%B8%96%E7%95%8C%E4%B8%BB%E4%B9%89&amp;variant=zh-cn" target="_blank">世界观</a>为左；国家利益为右</strong>：经济<a href="http://qq.ip138.com/w/index.php?title=%E6%B0%91%E6%97%8F%E4%B8%BB%E7%BE%A9&amp;variant=zh-cn" target="_blank">民族主义</a>或<a href="http://qq.ip138.com/w/index.php?title=%E8%B4%B8%E6%98%93%E4%BF%9D%E6%8A%A4%E4%B8%BB%E4%B9%89&amp;variant=zh-cn" target="_blank">贸易保护主义</a>在左右两派都可以发现，左派的保护主义是以确保国内的工作机会为目标，而右派的保护主义则是为了保护本国的公司和经济。<br />
<strong>认为人性和社会为可变性的为左；认为它们为固定性的为右</strong>：这是<a href="http://qq.ip138.com/w/index.php?title=%E5%85%88%E5%A4%A9%E4%B8%8E%E5%90%8E%E5%A4%A9&amp;variant=zh-cn" target="_blank">先天与后天</a>之间的争论例子之一。最先以此定义左右派的是美国经济学家<a href="http://qq.ip138.com/w/index.php?title=%E6%B9%AF%E7%91%AA%E6%96%AF%C2%B7%E7%B4%A2%E5%A8%81%E7%88%BE&amp;action=edit&amp;redlink=1" target="_blank">汤玛斯・索威尔</a>（Thomas Sowell）。<br />
<strong>认为人性本善为左；认为人性本恶为右</strong>：在这里左派认为<a href="http://qq.ip138.com/w/index.php?title=%E9%82%AA%E6%83%A1&amp;variant=zh-cn" target="_blank">邪恶</a>和痛苦是由社会的不公平所造成的，修正此问题便能使人性向善。而右派认为邪恶是人性必然产生的。可是，人性本善说有人必须因为好保护这样的保守的侧面，一方的人性本恶说有人必须因为坏改变这样的革新性的侧面。<br />
<strong>社会主义为左；资本主义为右</strong>：这是近代最广为所知的分法，多数媒体中提到左右派时也多半是指此种分法。这里的<a href="http://qq.ip138.com/w/index.php?title=%E7%A4%BE%E4%BC%9A%E4%B8%BB%E4%B9%89&amp;variant=zh-cn" target="_blank">社会主义</a>泛指高税收、高福利、高政府干预调控的政治体制，而不一定是<a href="http://qq.ip138.com/w/index.php?title=%E5%85%B1%E4%BA%A7%E5%85%9A&amp;variant=zh-cn" target="_blank">共产党</a>主张的<a href="http://qq.ip138.com/w/index.php?title=%E6%97%A0%E4%BA%A7%E9%98%B6%E7%BA%A7%E4%B8%93%E6%94%BF&amp;variant=zh-cn" target="_blank">无产阶级专政</a>。<br />
[<a href="http://qq.ip138.com/w/index.php?title=%E5%B7%A6%E6%B4%BE%E5%92%8C%E5%8F%B3%E6%B4%BE&amp;action=edit&amp;section=2" target="_blank">编辑</a>]历史起源<br />
左派和右派的称呼最初起源于18世纪末的<a href="http://qq.ip138.com/w/index.php?title=%E6%B3%95%E5%9B%BD%E5%A4%A7%E9%9D%A9%E5%91%BD&amp;variant=zh-cn" target="_blank">法国大革命</a>。在大革命期间的各种立法议会里，尤其是1791年的法国制宪议会上，温和派的保王党人都坐在议场的右边，而激进的革命党人都坐在左边，从此便产生了“左派”、“右派”两种称呼。<br />
在刚开始，这个政治光谱的定义是以人们对“旧政权”（<i>Ancien Régime</i>）的态度为判断标准，“右派”也因此代表着支持贵族、或教士利益，而“左派”则代表反对这些阶级的利益的人。在当时，支持<a href="http://qq.ip138.com/w/index.php?title=%E8%87%AA%E7%94%B1%E6%94%BE%E4%BB%BB&amp;variant=zh-cn" target="_blank">自由放任</a><a href="http://qq.ip138.com/w/index.php?title=%E8%B3%87%E6%9C%AC%E4%B8%BB%E7%BE%A9&amp;variant=zh-cn" target="_blank">资本主义</a>的人也被视为是左派，然而这样的立场在现代大多数西方国家则被视为是右派。不过即使是在法国大革命时，极端的左派也会要求政府干预经济以支援贫穷人民。<br />
而在<a href="http://qq.ip138.com/w/index.php?title=%E5%A4%A7%E4%B8%8D%E5%88%97%E9%A1%9B%E5%B3%B6&amp;variant=zh-cn" target="_blank">大不列颠</a>的同时，<a href="http://qq.ip138.com/w/index.php?title=%E5%9F%83%E5%BE%B7%E8%92%99%C2%B7%E4%BC%AF%E5%85%8B&amp;variant=zh-cn" target="_blank">埃德蒙・伯克</a>（他在现代通常被描述为保守派）也抱持着和法国大革命中自由市场左派类似的观点，不过，他也强烈批评他们的<a href="http://qq.ip138.com/w/index.php?title=%E5%8F%8D%E6%95%99%E6%AC%8A%E4%B8%BB%E7%BE%A9&amp;action=edit&amp;redlink=1" target="_blank">反教权主义</a>立场以及他们鼓吹暴力革命推翻长期政权的倾向。伯克在他的《对法国大革命的反思》（Reflections on the Revolution in France）一书中批评左派的理性主义过于极端、以及对于传统智慧的侮蔑。<br />
在法国大革命中，左派和右派的定义往往在不到一年内便完全转变。最初，在制宪议会上，<a href="http://qq.ip138.com/w/index.php?title=%E5%AE%89%E6%9D%B1%E5%B0%BC%C2%B7%E5%B7%B4%E7%B4%8D%E5%A4%AB&amp;action=edit&amp;redlink=1" target="_blank">安东尼・巴纳夫</a>（Antoine Barnave）和<a href="http://qq.ip138.com/w/index.php?title=%E4%BA%9E%E6%AD%B7%E5%B1%B1%E5%A4%A7%C2%B7%E6%8B%89%E6%A2%85%E7%89%B9&amp;action=edit&amp;redlink=1" target="_blank">亚历山大・拉梅特</a>（Alexandre de Lameth）等主张限制君主权力和建立一院制议会的领导人被视为是左派，而那些希望建立和英国类似的君主立宪政体的保守派领导人、以及彻底反对革命的人则被视为右派。到了1791年的立法议会，前述的这些党派―现在被称为<a href="http://qq.ip138.com/w/index.php?title=%E6%96%90%E6%8F%9A%E6%B4%BE&amp;variant=zh-cn" target="_blank">斐扬派</a>（Feuillant）由于支持君主政体和有限的选举权而被改视为右派。而在仅仅一年之后的<a href="http://qq.ip138.com/w/index.php?title=%E5%9B%BD%E6%B0%91%E5%A4%A7%E4%BC%9A&amp;variant=zh-cn" target="_blank">国民大会</a>上，原本因为支持透过战争将革命散布至国外、以及厌恶国王而在立法议会上被视为左派的<a href="http://qq.ip138.com/w/index.php?title=%E5%90%89%E5%80%AB%E6%B3%B0%E9%BB%A8&amp;action=edit&amp;redlink=1" target="_blank">吉伦泰党</a>人，现在则因为他们对于推翻君主政体的矛盾心理、反对处决路易十六世、以及对于巴黎市的厌恶而被视为右派。<br />
应该注意的是，在法国大革命当时，各形各色的政治党派都很少有经济政策上的差异。被视为左派的<a href="http://qq.ip138.com/w/index.php?title=%E5%B1%B1%E5%B2%B3%E6%B4%BE&amp;variant=zh-cn" target="_blank">山岳派</a>（Montagnards, 1792-1793）和右派的保皇派（Monarchiens, 1789）在经济政策上都是属于<a href="http://qq.ip138.com/w/index.php?title=%E5%8F%A4%E5%85%B8%E8%87%AA%E7%94%B1%E4%B8%BB%E7%BE%A9&amp;variant=zh-cn" target="_blank">正统的自由主义者</a>，尽管在1793年山岳派的确比其他党派更为愿意支持（暂时的）经济控制以争取群众支持、而且在山岳派内部也有一些左派的经济激进主义者以<i>Egalité</i>―重新分配财富作号召。<br />
相反的，在大革命中的意识形态差异主要是针对革命本身的态度：将革命视为是一场毁灭上帝和自然的可怕灾难、而这一切应该被结束才能恢复好的政府，又或者将革命视为是法国政治生活的必然特征。就大部分时候而言，几乎所有革命中的法国政治人物都对此抱持接近中间派的立场，但却在应该何时发起革命的问题上有不同意见。<br />
1794年，在<a href="http://qq.ip138.com/w/index.php?title=%E7%BD%97%E4%BC%AF%E6%96%AF%E5%BA%87%E5%B0%94&amp;variant=zh-cn" target="_blank">罗伯斯庇尔</a>于<a href="http://qq.ip138.com/w/index.php?title=%E7%83%AD%E6%9C%88%E6%94%BF%E5%8F%98&amp;variant=zh-cn" target="_blank">热月政变</a>中被推翻后，法国大革命终于稳定下来，这时更为清晰的政治光谱开始浮现―在左派的是之前支持罗伯斯庇尔和恐怖政治、并希望恢复1793年民主宪法的雅各宾派；而在右派的是希望君主复位的保王派―包括立宪派和专制派；而被归类为中间派的则是热月政变中的发动者，也是他们写下了1795年的宪法、并希望建立有限制的督政府共和制。督政府的失败并没有改变基本的政治生态―雅各宾派和保王派继续存在，而大多数最初支持督政府的人则改变为支持独裁者，最后导致了皇帝政权―<a href="http://qq.ip138.com/w/index.php?title=%E6%8B%BF%E7%A0%B4%E4%BE%96%C2%B7%E6%B3%A2%E6%8B%BF%E5%B7%B4&amp;variant=zh-cn" target="_blank">拿破仑・波拿巴</a>的诞生。<br />
也是在这个时候，法国国内有关左派和右派的政治光谱概念开始流传至欧洲各地。由于法国的大军不断攻克并强占法国之外的领土，左派和右派的分界再次改变为对法国大革命的态度，这大部分是由在政治上的结盟所判定。随着拿破仑的崛起，左派和右派的区分也变的更为复杂，因为这代表了在国外支持革命的党派也必须决定他们是否支持拿破仑的独裁政权。在同时，欧洲其他国家的传统统治者―无论是拿破仑在<a href="http://qq.ip138.com/w/index.php?title=%E5%A5%A7%E5%9C%B0%E5%88%A9&amp;variant=zh-cn" target="_blank">奥地利</a>和<a href="http://qq.ip138.com/w/index.php?title=%E6%99%AE%E9%AD%AF%E5%A3%AB&amp;variant=zh-cn" target="_blank">普鲁士</a>的敌人、或是在德国的附属统治者如<a href="http://qq.ip138.com/w/index.php?title=%E5%B7%B4%E4%BC%90%E5%88%A9%E4%BA%9E&amp;variant=zh-cn" target="_blank">巴伐利亚</a>，通常都选择吸收拿破仑和大革命中的某些经验，希望借由吸收那些中央集权的改革，能使他们的老旧政权转变为和拿破仑一般的强大，而无需经历大革命中的混乱和恐怖暴力。<br />
在拿破仑于1814年被击败后，欧洲的政治家们齐聚<a href="http://qq.ip138.com/w/index.php?title=%E7%BB%B4%E4%B9%9F%E7%BA%B3%E4%BC%9A%E8%AE%AE&amp;variant=zh-cn" target="_blank">维也纳会议</a>以商讨如何重组欧洲。会议并没有全盘恢复之前的旧政权，维也纳的保守派政治家们（例如<a href="http://qq.ip138.com/w/index.php?title=%E5%85%8B%E8%8E%B1%E9%97%A8%E6%96%AF%C2%B7%E6%96%87%E7%AD%96%E5%B0%94%C2%B7%E5%86%AF%C2%B7%E6%A2%85%E7%89%B9%E6%B6%85&amp;variant=zh-cn" target="_blank">克莱门斯・文策尔・冯・梅特涅</a>）所希望的政治制度是以保持秩序为目标，如果必要的话也可以谨慎的采纳法国大革命中的改革。而在法国，类似的潮流促成了波旁王朝<a href="http://qq.ip138.com/w/index.php?title=%E8%B7%AF%E6%98%93%E5%8D%81%E5%85%AB&amp;variant=zh-cn" target="_blank">路易十八</a>世的复位，而他本人也认知到，要彻底恢复以往的旧政权已经是不可能的事情了。<br />
[<a href="http://qq.ip138.com/w/index.php?title=%E5%B7%A6%E6%B4%BE%E5%92%8C%E5%8F%B3%E6%B4%BE&amp;action=edit&amp;section=3" target="_blank">编辑</a>]名称的发展<br />
左派和右派的称呼随着时间不断发展；从原本仅限于法国的用词传遍至整个欧洲（至少在欧洲大陆）、乃至于全世界的常用称呼。<br />
在19世纪早期的欧洲，各种政治生态都很容易套用左派-右派的政治光谱。极右翼的反动派势力期盼完全恢复旧政权，包括传统的特权以及中央集权的界线。虽然各地的政府经常会利用这些政治势力以保持政权，但却很少有政府会（最值得注意的例外是<a href="http://qq.ip138.com/w/index.php?title=%E8%96%A9%E4%B8%81%E5%B0%BC%E4%BA%9E%E7%8E%8B%E5%9C%8B&amp;variant=zh-cn" target="_blank">萨丁尼亚王国</a>）真的采纳反动派的政策。在反动派左侧的是来自于温和派的<a href="http://qq.ip138.com/w/index.php?title=%E4%BF%9D%E5%AE%88%E4%B8%BB%E7%BE%A9&amp;variant=zh-cn" target="_blank">保守派</a>，他们更愿意接受法国大革命的结果，尤其是那些会造成国家权力扩大的的成分，同时也支持独裁的中央集权统治―无论是以牺牲传统阶级或是自由派议会为代价。而在他们更左派的则是自由主义者，自由主义者希望一个代议制的政府以及对于公民自由的尊重。不过在实际上，自由派和保守派的界线往往相当模糊，尤其是在议会制的国家里，保守派在必要时通常会乐意和代议制的政府合作。而在自由派左侧的则是各种形式的激进主义者和共和主义者，他们希望彻底推翻君主政体，并建立普遍的选举制度―要不是类似1812年的西班牙宪法、便是如同1793年的法国宪法一般。<br />
随着时间的发展，左派里的某些“左派”成分逐渐浮现：<a href="http://qq.ip138.com/w/index.php?title=%E7%A4%BE%E6%9C%83%E4%B8%BB%E7%BE%A9&amp;variant=zh-cn" target="_blank">社会主义</a>和<a href="http://qq.ip138.com/w/index.php?title=%E5%85%B1%E7%94%A2%E4%B8%BB%E7%BE%A9&amp;variant=zh-cn" target="_blank">共产主义</a>。原始的左派、以及他们的激进主义和共和主义分支都坚持某些抽象的权利平等，依然被视为是左派的明确主张。不过，随着社会主义成分的逐渐增加，尤其是<a href="http://qq.ip138.com/w/index.php?title=%E9%A6%AC%E5%85%8B%E6%80%9D%E4%B8%BB%E7%BE%A9&amp;variant=zh-cn" target="_blank">马克思</a>的<a href="http://qq.ip138.com/w/index.php?title=%E5%85%B1%E7%94%A2%E4%B8%BB%E7%BE%A9&amp;variant=zh-cn" target="_blank">共产主义</a>政策在接下来一个世纪里的不断的转变为左派的中心，也因此到了<a href="http://qq.ip138.com/w/index.php?title=%E4%BF%84%E5%9C%8B%E9%9D%A9%E5%91%BD&amp;variant=zh-cn" target="_blank">俄国革命</a>的时候，许多人已经将“左派”一词限定为称呼共产主义者、或至少社会主义者。而在另一方面，尤其是在经济上，曾经被定义为左派的<a href="http://qq.ip138.com/w/index.php?title=%E8%87%AA%E7%94%B1%E6%94%BE%E4%BB%BB&amp;variant=zh-cn" target="_blank">自由放任</a>的观点则改被视为右派。而右派里的极端君主主义和<a href="http://qq.ip138.com/w/index.php?title=%E7%A5%9E%E6%AC%8A%E6%94%BF%E6%B2%BB&amp;variant=zh-cn" target="_blank">神权政治</a>成分则越来越罕见，并且在今日的西方国家几乎已不存在了。<br />
<a href="http://qq.ip138.com/w/index.php?title=%E5%B8%83%E5%B0%94%E4%BB%80%E7%BB%B4%E5%85%8B&amp;variant=zh-cn" target="_blank">布尔什维克</a>毫无疑问是在“左派”，而<a href="http://qq.ip138.com/w/index.php?title=%E5%8F%B2%E9%81%94%E6%9E%97%E4%B8%BB%E7%BE%A9&amp;variant=zh-cn" target="_blank">斯大林主义</a>、苏联式<a href="http://qq.ip138.com/w/index.php?title=%E5%85%B1%E7%94%A2%E4%B8%BB%E7%BE%A9&amp;variant=zh-cn" target="_blank">共产主义</a>的支持者也将他们自身视为“左派”。大多数西方国家的左派份子会争论道斯大林主义和毛泽东主义都只是表面上的左派，因为他们在实践上毫无经济的自由、而且也制造了严重的不公平现象。不过，许多欧洲的左派政党依然会与那些共产主义政党结盟以对抗右派。<br />
在不同的国家和不同的时间点，左派和右派通常都有不同的涵义，这种现象通常起源于19世纪末期的欧洲，自从那时开始，左派和右派的称呼就越来越少有明确的定义了。举例而言，当谈到1930年代的欧洲时，除了对于布尔什维克的反抗外，人们很难对“右派”的定义产生共识。虽然德国的<a href="http://qq.ip138.com/w/index.php?title=%E9%98%BF%E9%81%93%E5%A4%AB%C2%B7%E5%B8%8C%E7%89%B9%E5%8B%92&amp;variant=zh-cn" target="_blank">阿道夫・希特勒</a>和英国的<a href="http://qq.ip138.com/w/index.php?title=%E6%B8%A9%E6%96%AF%E9%A1%BF%C2%B7%E4%B8%98%E5%90%89%E5%B0%94&amp;variant=zh-cn" target="_blank">温斯顿・丘吉尔</a>在他们自己的国家都被定义为右派，但很显然这两位政治领导人之间有着巨大的差异，而且他们两人的<a href="http://qq.ip138.com/w/index.php?title=%E5%8F%8D%E5%85%B1%E4%B8%BB%E4%B9%89&amp;variant=zh-cn" target="_blank">反共主义</a>也都是以完全不同的方式表达。<br />
同样的，在<a href="http://qq.ip138.com/w/index.php?title=%E5%86%B7%E6%88%B0&amp;variant=zh-cn" target="_blank">冷战</a>中的美国，很少有社会主义的党派能浮上台面，而且即使是<a href="http://qq.ip138.com/w/index.php?title=%E7%A4%BE%E6%9C%83%E6%B0%91%E4%B8%BB%E4%B8%BB%E7%BE%A9&amp;variant=zh-cn" target="_blank">社会民主主义</a>也很少出现。相反的，<a href="http://qq.ip138.com/w/index.php?title=%E7%BE%8E%E5%9C%8B%E8%87%AA%E7%94%B1%E4%B8%BB%E7%BE%A9&amp;action=edit&amp;redlink=1" target="_blank">美国自由主义</a>―混合了自由主义和社会民主主义的意识形态，成为了选举中被称为左派的势力。而右派在原先欧洲的定义里，是与那些传统政治秩序的支持者相连结的，但在美国这却从来不曾存在过。在冷战期间里，几乎每个当选的政治人物都是采取反共主义的姿态；唯一的例外是在1960年代中旬，当反越战运动如火如荼时，一群<a href="http://qq.ip138.com/w/index.php?title=%E6%96%B0%E5%B7%A6%E6%B4%BE&amp;variant=zh-cn" target="_blank">新左派</a>就此浮现，在某些场合下还宣称他们是“反反共产主义”，但他们在大多数情况下也没有主动拥抱共产主义。<br />
同时在西欧，社会民主主义在这些西欧国家往往有极大势力、或甚至成为执政党，而共产主义政党在政治光谱上依然是重要的一个环节，简而言之，在美国被视为“左派”的选举政策在欧洲往往会被视为“中间派”。<br />
从1970年代末期开始、特别是1980年代，共产主义的支持度戏剧性的大为下跌，不只是在已开发的资本主义国家，在其他开发程度较低的国家、甚至是共产主义国家亦然。尤其是在<a href="http://qq.ip138.com/w/index.php?title=%E8%8B%8F%E8%81%94%E8%A7%A3%E4%BD%93&amp;variant=zh-cn" target="_blank">1991年苏联的彻底解体</a>后，“左派”一词就很少再用作形容那些支持<a href="http://qq.ip138.com/w/index.php?title=%E5%AF%AE%E5%9C%8B&amp;variant=zh-cn" target="_blank">老挝</a>、<a href="http://qq.ip138.com/w/index.php?title=%E5%8C%97%E9%9F%93&amp;variant=zh-cn" target="_blank">北朝鲜</a>、<a href="http://qq.ip138.com/w/index.php?title=%E4%B8%AD%E5%9C%8B&amp;variant=zh-cn" target="_blank">中国</a>的人了。不过这一词依然可以用作称呼许多社会主义的分支，这一词也经常用以称呼某些形式的<a href="http://qq.ip138.com/w/index.php?title=%E5%8F%82%E4%B8%8E%E5%9E%8B%E7%BB%8F%E6%B5%8E&amp;variant=zh-cn" target="_blank">参与型经济</a>或甚至是<a href="http://qq.ip138.com/w/index.php?title=%E7%B6%A0%E8%89%B2%E6%94%BF%E6%B2%BB&amp;variant=zh-cn" target="_blank">绿色政治</a>的支持者，而非中央集权的社会主义形式。<br />
如同左派一样，“右派”一词的涵义也不断改变。在19世纪末期的西欧，几乎没有任何人会支持要回复法国大革命之前的旧政权式社会架构，相反的，右派一词通常被用以称呼那些想要维护任何形式的君主政体或贵族政治的人、以及那些抱持保守派宗教观点的人、或者是那些仅仅想要防卫自1789年以来产生的中产阶级利益（而这在现代早已根深蒂固）的人。在<a href="http://qq.ip138.com/w/index.php?title=20%E4%B8%96%E7%B4%80&amp;variant=zh-cn" target="_blank">20世纪</a>前半期，革命性的右派<a href="http://qq.ip138.com/w/index.php?title=%E6%B0%91%E7%B2%B9%E4%B8%BB%E7%BE%A9&amp;variant=zh-cn" target="_blank">民粹主义</a>和<a href="http://qq.ip138.com/w/index.php?title=%E6%B0%91%E6%97%8F%E4%B8%BB%E7%BE%A9&amp;variant=zh-cn" target="_blank">民族主义</a>势力―尤其是<a href="http://qq.ip138.com/w/index.php?title=%E6%B3%95%E8%A5%BF%E6%96%AF%E4%B8%BB%E7%BE%A9&amp;variant=zh-cn" target="_blank">法西斯主义</a>也与传统的右派政治势力产生差异，直到今天这种差异依然存在。右派一词也被一些人用以形容极端的民族主义或<a href="http://qq.ip138.com/w/index.php?title=%E7%A8%AE%E6%97%8F%E4%B8%BB%E7%BE%A9&amp;variant=zh-cn" target="_blank">种族主义</a>政治势力。<br />
在近年来，新崛起的<a href="http://qq.ip138.com/w/index.php?title=%E5%85%A8%E7%90%83%E5%8C%96&amp;variant=zh-cn" target="_blank">全球化</a>和<a href="http://qq.ip138.com/w/index.php?title=%E6%96%B0%E4%BF%9D%E5%AE%88%E4%B8%BB%E7%BE%A9&amp;variant=zh-cn" target="_blank">新保守主义</a>被归类为右派，而“<a href="http://qq.ip138.com/w/index.php?title=%E5%8F%A4%E4%BF%9D%E5%AE%88%E4%B8%BB%E7%BE%A9&amp;action=edit&amp;redlink=1" target="_blank">古保守主义</a>”（Paleoconservatism）―或“老右派”一词在近年来则出现用以称呼<a href="http://qq.ip138.com/w/index.php?title=%E5%9C%B0%E6%96%B9%E4%B8%BB%E7%BE%A9&amp;variant=zh-cn" target="_blank">地方主义</a>、<a href="http://qq.ip138.com/w/index.php?title=%E5%AD%A4%E7%AB%8B%E4%B8%BB%E4%B9%89&amp;variant=zh-cn" target="_blank">孤立主义</a>、以及<a href="http://qq.ip138.com/w/index.php?title=%E5%8F%A4%E5%85%B8%E8%87%AA%E7%94%B1%E4%B8%BB%E7%BE%A9&amp;variant=zh-cn" target="_blank">古典自由主义</a>。<br />
