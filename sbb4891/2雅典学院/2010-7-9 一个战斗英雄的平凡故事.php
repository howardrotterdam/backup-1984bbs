													<h2>一个战斗英雄的平凡故事</h2>
						
						
																											<div id="postmessage_730472" class="t_msgfont">这年7月11日，解放军301医院，一个身患肺癌的病人在昏迷中不停地喊着：“首长，我被包围了，向我开炮。”“连长牺牲了，战士们全牺牲了，请求支援。”“报告首长，我突围了……”见此情景，围在他身边的亲人和总政的领导都哭了。<br />
<br />
病人叫张国福，40年前，他是全军闻名的特级战斗英雄。他参加过“三下江南”、“四保临江”、“辽沈战役”、“平津战役”、“渡江作战”、“进军四川”、“湘西剿匪”和“抗美援朝”等众多战役，因作战勇敢，屡建奇功，张国福先后荣立特等功一次、大功两次、小功五次，并荣获“四野”授予的“孤胆英雄”、“开路先锋”、“青年战斗英雄”等荣誉称号。他曾获得东北人民解放军英雄奖章两枚、全国战斗英雄代表大会纪念章一枚、毛泽东奖章一枚、朝鲜民主主义人民共和国独立奖章一枚……1950年9月，张国福出席了第一次全国战斗英雄代表大会，当时19岁的他是众多英雄中最年轻的一位。会议期间，张国福受到了毛泽东、周恩来、朱德等亲切接见<br />
<br />
但是，这样的一位英雄人物，复员回到地方，对赫赫战功只字不提，甚至连他的家人也无从知晓。他甚至将原名“张国富”中的“富”改为“福”，在地方一家火药厂默默埋头工作40载，直到女儿一次路过西安，顺道到父亲所在的47军军部打探父亲的过去，才发觉她的父亲非同一般的经历。<br />
<br />
参军仅半年，16岁的张国福就在一次战役中孤身活捉了国民党军队一名中将<br />
<br />
张国福是吉林榆树县新立镇人。1946年，他从家乡参加中国人民解放军47军，1948年入了党，先后任班长、排长、副连长等职。参军仅半年，16岁的张国福就在一次战役中孤身活捉了国民党军队一名中将。<br />
<br />
那是1947年5月，我军向东北国民党部队发起夏季攻势，张国福所在的独立三师九团负责攻打吉林市北面的江密峰。双方激战到下午3点多，还有345.6高地难以拿下。此时，一营长、三营长、一连长和三连指导员都牺牲了，战友们也成批地倒下，张国福也打红了眼，趁我军炮火暂时压住对方火力，突地从掩体里跃起，不顾一切地冲到了对方指挥所面前，举着手榴弹，大喊一声：“投降不杀”！国民党指挥所里的人都吓傻了，乖乖地当了这个16岁的小战士的俘虏，这其中就包括国民党中将赵伯昭。指挥所被占，剩下的敌人乱了阵脚，我军则一鼓作气攻上山头，全歼江密峰守敌。战后，《猛进报》以《16岁放牛娃张国富活捉国民党中将赵伯昭》为题，报道了这段传奇的战斗故事，一下子使张国福成了独立三师的名人，并且被记大功一次。<br />
<br />
“快命令你的部队放下武器，不然我就与你们同归于尽”<br />
<br />
张国福最大的一次战功是在解放东北全境的辽沈战役中攻打胡家窝棚廖耀湘司令部的战斗中。<br />
<br />
辽沈战役从1948年9月12日开始发动，在毛泽东“这次决战不能让蒋军出东北”的指示下，林彪、罗荣桓率领的东北野战军连克昌黎、北戴河、绥中、兴城、义县、锦州等地，蒋介石急调廖耀湘指挥5个军12个师共10万人的“西进兵团”前来助战，被我军拦截在黑山、大虎山一带，双方短兵相接，又一场恶战开始了。<br />
<br />
战斗打到“胶着”状态，张国福所在的部队知道敌军司令部就在他们战斗的一带。在攻打“胡家窝棚”时，敌人的阻击炮火十分猛烈，几十次的冲锋，都因战士伤亡惨重，进攻无果。又一次冲锋被敌炮火挡住后，张国福假装被击中，伏在一片阵亡的战友身旁，没有退回阵地。当部队再一次吹响冲锋号，距离敌人最近的张国福投出一颗手榴弹后，借着手榴弹爆炸的短暂片刻，手持爆破筒一跃而起，以迅雷不及掩耳之势冲进敌指挥所，向敌指挥官大喊：“快命令你的部队放下武器，不然我就与你们同归于尽！”就在敌指挥官被吓得不知所措的时候，身后的战友们已经冲了上来，敌指挥官一看大势已去，当即命令士兵停止抵抗。<br />
<br />
“胡家窝棚”战斗一举摧毁了廖耀湘的司令部，使廖耀湘的指挥部失去指挥能力，大大加快了整个战役的前进速度。张国福手持爆破筒冲入敌人阵地被指挥战斗的师领导在望远镜中看得清清楚楚，战后，师长夸张国福是个“小英雄”，张国福被评为了特等功臣。<br />
<br />
辽沈战役是我军对国民党军队进行的战略决战的第一个战役，历时52天，歼敌47.2万，一举解放东北全境。这一胜利加速了全国解放的进程。多年以后，在电影《大决战》之《辽沈战役》中，他们没有忘记那场对辽沈战役胜利起到特殊作用的“胡家窝棚”战斗；而在那场战斗中，只有张国福的战友们才知道，起着关键作用的，是张国福。<br />
<br />
全国解放以后，张国福作为全军78位特级战斗英雄之一，光荣地出席了1950年9月召开的第一次全国战斗英雄代表大会。会议期间，毛泽东、周恩来、朱德挨个与英雄们敬酒，亲切勉励他们“好好学习，艰苦奋斗，再立新功。”张国福的战友回忆说，会议期间，毛泽东还接了张国福、郭俊卿等四位英雄到他住处去吃饭呢。<br />
<br />
1951年，张国福随47军入朝作战，正值敌军发动大规模的“秋季攻势”，在临津江一带向我军进攻。当时参加进攻的“联合国军”有美骑一师、美三师、美二十五师，英联邦师、李承晚军第九师、泰国团、土耳其旅及希腊营，还有200余辆坦克，300余门火炮。每一场战斗都像“上甘岭”一样残酷。张国福所在的连在345.6高地上坚守七天八夜，在敌人日夜轮番狂轰滥炸下，全连战士除了张国福，全部阵亡，但就在张国福一人坚守的阵地上，也未让敌人攻上高地。<br />
<br />
这次战斗中，胸部负重伤的张国福被转回国内，在长春疗伤。<br />
<br />
几十年间，不要说单位的同事，就连他的儿女，只知道父亲是一名退伍军人，别的都一无所知。<br />
<br />
从长春治疗康复后，张国福回到部队后就被送到湖南衡阳军校深造，但培训还没结束，张国福就提前结束了军校的深造，复员回了地方。<br />
<br />
张国福回到原籍吉林榆树后，当地政府对家乡的战斗英雄非常重视，经常请他作报告，讲述战斗经历，还准备安排他到县机关领导岗位工作。张国福却一心只想在家务农，不想做官，看看每天都生活光环中，在家乡实现不了自己的愿望，他就将原名“张国富”改为“张国福”，到了需要工人的鹤岗，在矿务局消防队干起了消防员。消防队解散后，他便留在矿务局所属的火药厂当了20多年的火药工，不是制造火药，就是坐闷罐车押运火药，再不就是装卸火药，直到1986年退休。<br />
<br />
张国福到鹤岗时，档案、户口什么都没带，更别提复员证、立功证了。正是“悄悄合上功劳簿，隐瞒了累累战功”，做一名普通得不能再普通的劳动者。几十年间，不要说单位的同事，就连他的儿女，只知道父亲是一名退伍军人，别的都一无所知。<br />
<br />
后来他对儿子解释说他回来的动机，他说曾经与他一同出生入死的战友都不在了，他不想借牺牲的战友们的光留在部队享受他们给我带来的荣誉。战争的胜利不是一人之功，合上功劳簿，我就是一个普通的劳动者，我要用自己的双手去劳动，去创造和平生活。<br />
<br />
在军校深造时，张国福收到许多各地的女大学生给他写的信，表达了对英雄的爱慕之情，但朴实的张国福还是与同一村的邻家漂亮姑娘任宝莲结了婚，过起了平凡的生活。很长一段时间，张国福一家7口挤在12平米的平房里，从进厂到退休，他都是四级工，从未涨过。5个儿女大多自谋职业，最困难的时候，张国福就带着十几岁的儿子天不亮捡煤矸子，省下班上发的饭拿回家给孩子吃。<br />
<br />
张国福说，他哭，是因为他怕那些舍生忘死的战友打下的江山就这么被贪官蚀空、糟毁啊。<br />
<br />
一直觉得父亲有着不平凡的经历的四女儿一次路过西安，听父亲说47军军部驻扎在那里，就抱着试试看的心理，到西安47军军部探访，了解一下父亲的过去。<br />
<br />
在47军部的荣誉室里，当讲解员向他讲起“孤胆英雄”张国富时，她惊呆了，墙上光荣榜那张照片那么熟悉，多么像父亲年轻时的照片。讲解员告诉她，张国富的英雄事迹不光47军史上有记载，还以《孤胆英雄张国富》为题上过小学课本呢。女儿经过仔细辨认，最后确认英模榜上的张国富就是自己默默无闻的父亲张国福。<br />
<br />
从朝鲜战场回来的张国福只所以再也不向人提起他的过去、他的经历，实在是那场战斗在他心中留下太深的记忆。儿女们知道了他的辉煌过去，他再也不能隐瞒了，但他一提在朝鲜的那次战斗就哭，身边的人全没有了，枪林弹雨中，尸肉横飞，全连战友都牺牲了，他爬着回来向师长报告，说他们连基本完成了任务，师长指挥员们都哭了。<br />
<br />
再后来，儿女们也就知道每次看到电影电视中战士牺牲的场面时父亲为什么哭了；看到贺龙、陈毅等军队高级将领挨整遭辱时父亲为什么哭了；但张国福看到高级干部贪赃枉法受处理时他也哭，儿女们就不明白了。最终，女婿忍不住去问他。张国福说，他哭，是因为他怕那些舍生忘死的战友打下的江山就这么被贪官蚀空、糟毁啊。<br />
<br />
张国福住院期间，部队领导多次问他对组织上有什么要求，张国福回答只有一句话：“希望我们党和国家好”<br />
<br />
1997年，张国福经常咳嗽不止，到医院检查后发现，胸部已有积水。当时他的几个子女都已下岗，家庭经济拮据，很难拿出一笔钱为父亲治病。这时，儿女偷偷瞒着张国福找到父亲原部队的老上级、老战友，而40年间，张国福原团政治部主任、国家核工业部总公司核动力研究室设计院原党委副书记穆建华和老团长、基建工程兵副主任黎原以及47军编写军史的同志也都在苦苦地寻找这位战功卓著、却突然消失了的战斗英雄。<br />
<br />
穆建华、黎原闻讯后，迅速将张国福接到北京，安排他住进解放军301医院。专家打开张国福的胸腔后很是吃惊，张国福的肺由于长年在火药厂工作，再加上多年战场上炮火熏染以及朝鲜战场上那七天八夜弹火下战斗，吸入大量的火药烟尘，他的肺膜厚得难以想像。<br />
<br />
总政的领导来了，总参的领导来了，他们多次前来看望这位高风亮节、不居功、不自傲的特级战斗英雄。张国福流着泪说：“40年了，我就是想让大家忘了我，可你们还是没有忘记我。领导还记住我，我十分感激。”别的，他像从前一样，就不知道说什么好了。<br />
<br />
张国福住院期间，部队领导多次问他对组织上有什么要求，张国福回答只有一句话：“希望我们党和国家好。”<br />
<br />
1998年7月11日，享受副师级待遇没多久的张国福在301医院永远合上了双眼。<br />
<br />
张国福的骨灰按照解放军三总部的意见，准备安放在八宝山革命公墓，但张国福临终时说：“我在鹤岗搞了一辈子火药，还是让我回鹤岗吧。”他的骨灰被放到了第二故乡鹤岗殡仪馆烈士陵堂。<br />
<br />
张国福身上有中国人最朴实的本质，在张国福弥留之际，他仿佛又回到了战火纷飞的年代，军号响了，奋不顾身冲锋陷阵，舍生忘死喊出“向我开炮”的可歌可泣感人场景。<br />
<br />
张国富，一个属于战争年代的特级战斗英雄；张国福，一个在和平年代所作所为并不逊色于他在战争时期壮举的英雄！！<br />
<br />
我无意间看到了这篇文章,我也相信当时那些年轻人的追求是高尚的,他们为他们认为的革命事业奋斗直到付出自己宝贵的生命.我们也看到国家目前实际上已经满目疮痍,整个精神状态已经处于游离阶段.<br />
我很希望国家能出很多这样的人,为我们追求的东西的东西奋斗再奋斗!</div>

							
							
							
							
