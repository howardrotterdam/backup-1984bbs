													<h2>黄万里“逆调节”理论被长江洪水验证</h2>
						
						
																											<div id="postmessage_756265" class="t_msgfont">什么叫“逆调节”？就是丰水期需要三峡水库蓄水的时候它要泄洪；枯水期需要三峡水库泄水的时候它要蓄水。<br />
<br />
&nbsp; &nbsp; 7月17日下午，三峡大坝开启4个泄洪深孔和1个排漂孔。中国长江三峡集团公司预测未来2-3天内，三峡大坝将迎来一次峰值在65000立方米/秒左右的洪水，堪比1998年长江三峡河段的最高峰值，这也将是三峡水库建成以来所面临的规模最大的一次洪水挑战。<br />
<br />
&nbsp; &nbsp; 黄万里先生反对建三峡的时候就有一个理论，库尾梯度理论。就是说三峡的水不是平面如镜，而是有一个水力梯度。比如坝前的水位175米高程了，坝尾也就是重庆朝天门的水位可能就在200米高程了。由于所有的计算都是按照镜面175米最高计算的，当然不排除故意这样计算来减少移民和工程预算，以利获得人大通过。这也就是直到今天仍然移民不止的原因，工程支出不停的增加。这也正是不论下游是否洪水滔天，三峡也要泄洪的原因。再不泄洪，上面的水下来了，库里水到了175米时，重庆已经被淹了。<br />
<br />
&nbsp; &nbsp; 黄万里老先生的预言：三峡水库对水量的调节是一种“逆调节”。什么叫“逆调节”？就是丰水期需要三峡水库蓄水的时候它要泄洪；枯水期需要三峡水库泄水的时候它要蓄水。<br />
<br />
&nbsp; &nbsp; 前两年的冬天，洞庭湖流域、太湖流域遭遇“百年一遇”的枯水期，湖面大幅度缩小，船只不能航行，三峡的水如期而至了没有？没有。它要发电。<br />
<br />
&nbsp; &nbsp; 这次，江西、湖南、湖北的汛情开始紧张，它蓄水了没有？没有。它要泄洪。<br />
<br />
&nbsp; &nbsp; 水库的防洪能力取决于防洪库容的大小，而水库库容的大小又取决于水库蓄水位的高低。三峡刚要上的时候，说的是正常蓄水位175米。按照这个算的发电、移民、防洪（特别说明是对中下游）的好处。有新华社为证。<br />
<br />
&nbsp; &nbsp; 由于水力坡度等原因，实际上是蓄不到那个高度的。到了２００１年１１月３０日，中国三峡工程日报发表了记者陈平采访长江水利委员会库区处汪小莲总工程师的报告：<br />
<br />
&nbsp; &nbsp; 当三峡水库蓄水至海拔１３５米时，库区二十年一遇（７２３００立方米／秒）的来水的设计回水水面线为∶<br />
<br />
三斗坪三峡大坝坝址∶１３５米； <br />
秭归老县城∶１３６。１米（距三峡大坝坝址３７。６公里，水位上升１。１米） <br />
奉节县城∶１４６。７米（距三峡大坝坝址１６２。２公里，水位上升１１。７米）<br />
忠县∶１５４。８米（距三峡大坝坝址３７０。３公里，水位上升１９。８米） <br />
丰都县城∶１５７。８米（距三峡大坝坝址４２９公里，水位上升２２。８米） <br />
涪陵１６６。２米（距三峡大坝坝址４８３公里，水位上升米３１。２米）<br />
<br />
&nbsp; &nbsp; 水库影响末端在涪陵李渡镇，高程为１６９.４米（距三峡大坝坝址４９３.９公里，水位上升米３４.４米）。<br />
<br />
&nbsp; &nbsp; 这些还是135米时候的数据。也就是说，发20年一遇大水的时候，三峡如果蓄到175米，重庆就淹没了。<br />
<br />
&nbsp; &nbsp; 也许是因为看到了这些数据，在这篇报道之前，温善章，曾前后质疑过三门峡和三峡的老科学家，就已经呼吁说：三峡工程的正常蓄水位应降为不超蓄的155m （科技导报, 2001年8期）<br />
<br />
&nbsp; &nbsp; 总之，大水时到175，才能最大程度为下游防洪，但此时回水就淹没了重庆。枯水时到175，上游可能安全，但下游没有水，船都浮不起，谈何泄洪？在2006年大旱的时候，抓紧时间冲了一下156，宣布涪陵成为深水良港了，赶紧撤。2009年又大旱，赶紧冲175，但还没达标，下游河道航运就中断了。<br />
<br />
&nbsp; &nbsp; 所以当时大旱，人们纳闷说为什么大旱要升水位？而且说是纯粹为了发电也冤枉三峡大坝了。<br />
<br />
&nbsp; &nbsp; 现在大家都意识到，三峡只能存150米左右的水，不能指望它有什么防洪、航运的效益，只要它不为害就好了。慢慢地，我们不再听到“正常蓄水175&quot;，而变成”千年一遇的洪水才175“。175 的梦想退却后，曾有一段安抚大家说“前期正常水位156”，这次看来，连156都不行，因为n年不遇的正巧都遇上了。这就是三峡146米泄洪的原因。它实在不行了。<br />
<br />
&nbsp; &nbsp; 另外要说的是，三峡库区本来的长江段，也是有自然防洪能力的，被替代了――或者说吞噬了。<br />
<br />
&nbsp; &nbsp; 最痛心的是，很少有什么事情，大家的预测、预警这么准，这么精确，但不该发生的都发生了。<br />
<br />
&nbsp; &nbsp; 最近几天，三峡拆东墙补西墙，存点水存不住了放一点，下游告急了再存一点。<br />
<br />
&nbsp; &nbsp; 98年洪水的时候曾设想，如果有三峡工程的话，可以防止洪灾，在三峡工程竣工时，也表示能防止千年一遇的大洪水，但今年却还是重复了12年前的一幕。水库从上月汛期以来一直泄洪，印证了毕生反对三峡工程的已故水利专家黄万里“逆调节”的理论，即在枯水期 蓄水、汛期时泄洪，三峡大坝的防洪功能形同虚设。<br />
<br />
&nbsp; &nbsp; 据民政部统计，7月1日以来，暴雨已致云南、重庆、湖北等10个长江流域省市3230万人受灾、 118人死亡、47人失踪。重灾区湖北省有五大直接与长江相通的湖泊，以及逾五500座水库超出警戒水位；湖南省洞庭湖和鄱阳湖水位上涨，被赋予调蓄滞洪 功能的黄盖湖超出保证水位30米，随时可能大泛滥。多日来，不断有民间专家、网民发表文章批评，长江下游汛情告急之际，三峡大坝却不断向下游泄洪。<br />
<br />
&nbsp; &nbsp; 水利部门数字显示，三峡水库的水位，6月底至今每日变化不大，显示大坝不断排水。截至7月中旬，三峡上游水位是149米，较正常蓄水位175米低26米。当年，三峡工程设计、施工时，官方不断强调工程是防洪为主、发电次之，但舆论质疑为保发电安全不肯腾空水库。三峡集团解释三峡工程不能只蓄不泄，要做好洪水 再次来临准备，又强调三峡防洪能力有限，不能完全解决长江防洪问题，不能使长江防洪一劳永逸。但在下游人民群众生命财产寄于一线之际，被委以重任的三峡工程为什么就不行了呢？</div>

							
							
							
							
