																											<div id="postmessage_477420" class="t_msgfont">《零八宪章》论坛强烈抗议对刘晓波的枉法判决 <br />
 <br />
作者：《零八宪… 文章来源：《零八宪章》论坛 点击数：40 更新时间：2010-2-11 15:01:05&nbsp;&nbsp;<br />
 <br />
<br />
<br />
2010年2月11日上午，北京市高级法院对刘晓波“煽动颠覆国家政权罪”案作出“驳回上诉，维持原判”的二审宣判。《零八宪章》论坛对中国当局这种坚持罪错、拒斥文明、敌视人权、践踏宪法的行径提出强烈抗议并发表严正声明！<br />
<br />
刘晓波先生因参与起草和发起《零八宪章》而于2008年12月8日晚上11时，被北京市公安局国保总队的警员从住所带走。同时国保警察还对刘晓波先生的家实施了长达十多个小时的搜抄，将电脑、手机、书籍、打印的《零八宪章》征求意见稿和大批私人物品抄走，而且警方没有出示合法手续。直到2009年3月20日，刘晓波的妻子刘霞女士才接到警方口头通知，说对刘晓波实施的是“监视居住”。6月23日，北京市检察机关以“涉嫌煽动颠覆国家政权罪”批准逮捕刘晓波先生，后来以刘晓波先生多年来撰写的六篇文章与参与发起《零八宪章》而于12月2日提起公诉。北京市第一中级法院于12月23日开庭审理了刘晓波先生“涉嫌煽动颠覆国家政权”案。12月25日上午，北京市第一中级法院以“煽动颠覆国家政权”的罪名判处刘晓波有期徒刑11年，剥夺政治权利2年。刘晓波博士提出上诉，结果北京高院作出维持原判裁决。<br />
<br />
刘晓波先生1955年12月28日出生于吉林省长春市。北京师范大学文学博士。曾担任独立中文笔会理事、会长。1989年因参加民主运动，于当年6月6日至1991年1月被关押于秦城监狱。1989年9月被开除公职。1995年5月18日至1996年1月，因从事自由写作及民运、呼吁为“六四”平反、保障人权而被囚禁，完全失去自由。后因执笔《反腐败建议书――――致八届人大三次全会》和《汲取血的教训 推进民主与法治进程――“六四”六周年呼吁书》，而于1996年10月8日至1999年10月7日被劳动教养3年。长期来他坚持独立的民间批判立场，发表了大量针砭时弊的文章，让当局很是忌恨，也因此经常受到当局的骚扰和软禁。<br />
<br />
《零八宪章》完全是一份和平、理性、善意而富有建设与前瞻性的对中国问题的思考与表达，参加联署的公民完全是依法践行自己言论与思想自由的权利，表现公民对中国社会的深切责任。刘晓波先生仅仅因为践行中国《宪法》赋予的权利，背负一个知识分子的良心，本着“我没有敌人”的博大情怀，参与起草和发起《零八宪章》，竟遭到政府的违宪重判。在国际国内普遍质疑与抗议声中，北京高院二审仍支持原判，这严重违反了《世界人权宣言》第十九条“人人有权享有主张和发表意见的自由；此项权利包括持有主张而不受干涉的自由，和通过任何媒介和不论国界寻求、接受和传递消息和思想的自由”的规定；违反中国《宪法》“保护人权”与第三十五条规定：“中华人民共和国公民有言论、出版、集会、结社、游行、示威的自由”；也严重违反中国政府自己颁布的《国家人权行动计划》中明确表示尊重公民的“知情权、参与权、监督权与表达权”、“采取有力措施，畅通各种渠道，保障公民的表达权利”的承诺；同时也与中国当局一再宣示的“以人为本”、“依法治国”、“构建和谐社会”的精神相背离。<br />
<br />
《零八宪章》论坛强烈要求中国当局维护宪法尊严，树立法治精神，立即释放刘晓波，并追究侵犯人权、制造冤案的相关责任人！同时，我们正告中国当局应该清楚地认识到尊重人权和保卫人权的历史潮流已是浩浩荡荡，顺之者昌逆之者亡。面对中国必然走向人权法治、宪政民主的大趋势，那些顽固坚持独裁专制的错误立场是绝对没有出路的！<br />
