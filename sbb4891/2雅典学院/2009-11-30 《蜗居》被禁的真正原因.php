													<h2>《蜗居》被禁的真正原因</h2>
						
						
																											<div id="postmessage_349316" class="t_msgfont">听说电视连续剧《蜗居》被禁了，据说是因为台词有点黄。果真如此吗？带着好奇，我从网上在线看了《蜗居》。一集一集地、认认真真地看了一遍。边看边同情、边看边感叹，边看边思考。不把心里的话说出来，我闷得慌。<br />
<br />
　　我看《蜗居》的被禁，跟台词黄不黄没关系，大多是个禁它的借口。再热播下去，会动摇那个党的执政基础，怕要出大乱子。一个月来，这部电视剧引起了很多人的关注和网上的热烈讨论。它将镜头对准了大城市中不那么光鲜的一面：房价的上涨以及由此给年轻人的理想造成的巨大冲击。我们自己的生活就像剧中描写的一样，一切都暴露在了阳光下。剧中把房子带来的社会问题推上极致，这种残酷的生活直抵每一个因房价而困扰生活的人。<br />
<br />
　　一项36万多人参加的投票调查中，大多数人认为“幸福与房子关联密切。” “还房贷、吃盒饭”，已经成为房价飙升年代对白领生存状况的一种直白描摹。主人公一波三折的买房奋斗史，道出了都市无房族的困惑：房价是“一匹脱缰的野马”，攒钱的速度永远赶不上房价上涨的速度。 <br />
<br />
　　有人甘心做房奴吗，不买房子不行吗？答案是，不行！谁会租房租一辈子？你要说，没钱你就去郊区甚至农村买房子呀。对，可以在郊区或农村买到便宜房子，可是你的工作单位在市区，你怎么办？是啊，人民有广泛的自由，有选择的权利。可是，你要上学，就得接受高学费，你要看病就得接受高收费，你要住房就要选择高房价，除非你不让孩子上学、不去看病，不住房子。你有不选择的自由吗？没有。买房是社会逼的，是形势逼的，是必需的，你可以选择这，选择那，你能选择不住房子吗！<br />
<br />
　　那，是谁逼着群众做了房奴？是垄断阶层，是官商勾结，是政治腐败！国家是人民的，资源是国家的,理应由人民共享。可是利益集团利用人民的资源，凭借其垄断地位要挟人民。看看现实,看看中石油、中石化, 领着巨额财政补贴,买楼是10亿10亿的买，不涨价就断你车子的油、不涨价就断你家的气,人为制造紧张。中国移动,传播黄色淫秽网络的先锋,哪里还有一丁点的社会责任；有线电视,大家都看见了,独此一家,不用也得用,不允许你安装卫星电视,查处你！诸如此类,实在太多。还有无耻的专家为他们摇旗呐喊：不能因为穷人喝不起水就不涨水价,中国的电价严重偏低。涨价是为了节约资源，更好地服务于人民,能力外的资本为零,哈哈,令人笑掉大牙……见过无耻的,没见过这么无耻的。<br />
<br />
　　看看剧情吧，权力支配一切，资本动摇人性，利益逼良为娼。权贵阶层可以随便劫人祖居、淫人妻女、左右一切。同样是过年，富人在富丽堂皇充满温馨的大房子里，穷人没水没电点着蜡烛苦熬；群众顶着加班的压力努力地工作，不过取得些微薄的收入，而权贵的二奶买件衣服随便一出手就成千上万！这是我们要的和谐社会吗？<br />
<br />
