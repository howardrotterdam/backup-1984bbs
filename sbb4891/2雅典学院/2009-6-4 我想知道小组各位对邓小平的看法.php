1、结束了和西方国家对抗的状态，改为既合作又对抗的状态。这个好处就是修改了毛时代那种倾向第三世界和共产主义大家庭的政策，也就是中国政府学会傍“大款”，而不是一味找穷亲戚。毛时代虽然有“尼克松访华”，但那只是策略而不是国家战略。邓使中国接近正常国家，国家利益高于意识形态，比毛时代有进步。<br />
2、国家没有重复大规模的政治斗争，恢复到重视国民生产的正确路线。虽然党内有倾轧和摩擦，但是大规模的“肃反”没有出现，执政党也没有出现向左、向右的急转，基本保持政策平衡，陆肆我在后面会分析。解决老百姓的吃饭问题，提高经济水平，这个是应该肯定的。<br />
邓的错误：<br />
1、首先我觉得从邓的局限性应该从中国的时代背景来分析，而不应该评价他脱离时代。邓是出生在封建时代，长期熏陶在毛氏独裁的政治气氛中，而被毛奉为圣典斯大林主义本质是抵制自由和平等，所以邓不会形成自由平等的意识，这不是他的错，是时代和社会造成的。邓在毛时代后期屡次被打击，沉沉浮浮，和毛当年在井冈山很相似。于是两人当政后也会有相似表现：权利欲望大，听不进异议，没有安全感，容易激动。这个是经历造成。<br />
2、我认为共产主义没有什么开明派、顽固派之分，所有的执政者都是两而兼有，因为共产主义实质是国家垄断，于是所有的经济政策都是一种调整，一种姿态。如果打破国家对资源和社会财富的垄断地位，才是真正的自由经济。所以，自由派的领导人在共产党内不可能产生。实质上胡耀邦、赵紫阳算不上自由派，只算在僵化的体制内有人性、比较本色的领导人。<br />
3、邓的错误其实是没有将国家前期的错误进行全民反思，特别对文革和毛的反思，采取敷衍的态度，当然对于毛错误的反思会使党的执政地位动摇，没有反思最大的后果是，以后政治党、领导人任何错误，都采取繁衍的态度，甚至打击异己。这是造成中国不诚信的根源，就使执政党对民众不诚信。<br />
4、邓的错误其它人也提到，就是共产党腐败。其实我觉得这是党的执政地位决定的，其实跟邓没绝对的关系，我们的党是&quot;永远&quot;坐庄的，所以无论对错，都没惩罚。而且司法、行政都是一个系统的人，无法形成公正。各级政府都是独裁的小世界，贪污也就难免了。但是邓的错误是没有选好接班人，造成局面加剧恶化。<br />
5、关于陆肆我也想说我的心里话，其实我个人认为当年学生也有错，请大家听我解释，我对于党采用血腥的镇压也是极其愤怒，今天也是纪念的日子。但是我觉得，陆肆中学生也有过激的地方，第一：天安门不仅仅是北京的标志，也是国家的标志，更是政权的标志、学生长期在天安门和政府对峙，其实影响的是共产党的国际形象，不管你解释没有颠覆政府的意图，但是长时间对峙，只会让共产党高层痛下杀手。我认为运动应该有度，对于国际影响的标志性地区不应该采用和政府对峙这种极端的做法，如果学生在学校或者城市的一般广场公园，相对来说共产党的态度会温和点。第二：运动显得很毛躁，领导运动的人又缺乏才能，不懂得政治技巧，只会煽情.其实政治运动和经商一样，实质就是讨价还价，朝有利自己的方向努力。但是我看到相关资料，只是学生从激动到更激动，政府从妥协到强硬的过程。而且领导运动的人，没有切实可行的”平民情愿“内容，而只是一味说自己代表正义，而且将运动扩展到全国，造成轰动,从而加剧共产党的恐惧。当政府妥协时没抓住机会将局面缓和，而是加剧矛盾。这其实都是”文革“思想的延续。也造成悲剧。第三，学生已单薄的身体对抗全副武装的军队，既悲壮然而又不智，烧车，扔石头只会让政府认定学生是暴民，而不是和平请愿的人。说句题外话，我觉得目前邓玉娇案也有类似的情况，很多人希望邓玉娇无罪出发点是好的，但是，邓玉娇其实触到了政府的神经，就是平民对于官员压迫的反应，杨佳案肯定是杀无赦，邓玉娇案其实因为审理时间接近陆肆，政府高层有些紧张，而且广大网友也有力声援，于是政府已经妥协。但是，有些人还觉得要趁热打铁，继续跟政府施压，我觉得如果那样，局面很可能会朝不好的方向发展。而邓的案件，是尽量让邓的代价最小，政府有合适的台阶下。而不是让政府没台阶下。<br />
