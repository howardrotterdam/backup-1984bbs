&nbsp; &nbsp;杨佳是否罪犯，取决于是否存在公正司法及适用何种法律这一大前提。在一个正常的司法保障正义维护公道的功能仍正常发挥的社会，杨佳的行为构成犯罪当然不成问题；然而在一党专制条件下，在中共独裁撑控公检法司监狱等一切国家暴力机器，完全不存在独立司法，且没有独立新闻媒体监督制约的前提下，根本不可能存在法治及司法公正。完全反映中共独裁意志的中共恶法本身极度不公不义，且是由一班丧失了独立精神也毫无独立审判权的伪法官审案，其结果可想而知。吾以为杨佳案应当适用自然法，因而无罪。因为人格尊严与正义是任何社会任何时代一切法律理应有的第一原则。在一个公正正义公道不复存在的社会，既存的法律已不成其公众必须遵守的人们行为规则，此种情况下，中共社会实质上犹如原始社会，自然法当然得以适用。如果由作为中共统治工具的法院适用中共恶法，杨佳当然会被当作十恶不赦的罪犯。问题在于中共一党专制下的法院，早已丧失维护社会公正正义与公道的正常功能，在很大程度上法院仅是中共专制暴政欺压国民的工具，而非裁判是非，维护社会正义与公道的最后裁决者。杨佳在法庭上一针见血地指出：“警察之所以敢于作恶，是因为他们后而有你们！”因此不分青红皂白，不问前因后果，以杨佳客观上致死了警官便认定其犯罪，至少不能令人信服。犯罪是经正当司法程序审判认定的危害社会的行为。杨佳案的两级法院审理闹剧明显可见其极其严重的非法性和荒唐性，而杨佳英勇反抗暴政的行为实质上是人之为人不可剥夺让与的天赋人权。简言之，是中共恶警违法犯罪在先，严重侵害了杨佳的人格尊严与人身权利，且蛮横无理拒绝予受害人杨佳公道，面对一个司法黑暗严重不公的现实，杨佳正确地判定根本不可能依赖媒体或通过司法途径讨还公道，因此他以自己的方式寻求与讨还公道，谈不上犯罪。至于因此而成中共专制暴政替罪羊的六名警察，当然值得人们同情，但他们不值得人们敬重。<br />
<br />
&nbsp; &nbsp;余杰在其妙文中还有下列对民运人士的荒唐指控及似是而非的谬论：<br />
<br />
&nbsp; &nbsp;余耸人听闻地称：“杨佳刺杀警察案件是一标志性的事件，当杀人者被某些自以为大义凛然的作家、学者和律师誉为大侠和英雄并得到一定的民意支持的时候，一个‘前法西斯时代’的民意场正在迅速形成。”首先，杨佳本意并非杀警察，而是以他自已的方式找上海督察讨还公道。尽管出现了六名警察因此死亡的惨局，若杨佳真想杀警，他为何不杀北京警察？为何不杀女警或青年警察？为何不在警察局一楼施展身手大开杀诫而非要爬上21层高楼的警督室？为何随身携带七千余元现金及三万多元的信用卡？我认为“你不给我一个说法，我就要给你一个说法”，吴警督之“要么你顶死我，要么我顶死你！”“在这样的警察国家，连二十几年遵纪守法的人也难免进监狱”“我无罪，他们才真正有罪！”杨佳当庭自辩中业已清楚地表明其行为的真实目的在于讨还公道。余杰实质上是孤立地看待杨佳事件，而割裂该案发生的时代历史与现实大背景。<br />
<br />
&nbsp; &nbsp;余说“某些反对奥运会的民运人士遂对此事件幸灾乐祸，并试图从唐永明失业工人的身份之中寻求其行凶的合理性”这里余似有趁机恶意攻击民运人士之嫌，到底是“某个”还是“某些”民运人士或是余先生的编造？敬请余先生指证哪位民运人士对唐永明事件“幸灾乐祸并寻求其行凶的合理性”，南郭根本不会为唐这种不敢反抗暴政镇压者却对无辜公众甚至外国人施暴的怯懦之徒抗辩。<br />
<br />
&nbsp; &nbsp;余指称“暴戾之气的泛滥，其原因之一便是知识分子的缺席与沉默。”姑且不论余杰本末倒置，因为暴戾之气的泛滥主因是中共专制暴政长期奉行的暴力镇压政策且民众有冤无处申，因为中共法院完全丧失了维护正义与公道的正常社会功能使然，而决非被欺凌的弱势民众的零星反抗所致。远的不提，近年来中国发生的众多恶性人权案件，中共酷刑大案要案更多如牛毛，余作为著名文人，对上述众多案例不可能不知情，但我从未看到他为包括郭飞雄，高智晟，众多法轮功学员及家庭教会成员在内的中共酷刑受害者作过任何呼吁更不用说专论。对近年来人权律师受到中共暴力殴打，秘密绑架，枉法裁判的众多案例，我同样未看到余杰为之抗辩的片言只语！死于中共酷刑的法轮功学员高达三千之众，我未见余杰发表任何相关评论，反而却有余杰陈列众多李洪志先生“侮辱、诽谤”基督教及耶苏的声明；而死于中共酷刑的家庭教会成员更高达一万两千之众，我同样未见余杰为此谴责中共暴政。其实在中共专制暴政下，真正的知识分子在国内根本无法生存，要么被阉割了真精神或自宫，要么被迫自我限制，不说超越当局许可底线的话；我完全理解此种自保措施。因此当代中国真正知识分子大多被迫流亡海外，仲维光、曹常青、袁红冰等才堪称真正的中国知识分子。<br />
<br />
&nbsp; &nbsp;“而今中国的杨佳、唐永明等刑事案件的罪犯，被某些自诩为正义代言人的人士拔高为‘反共斗士’，由此看出反对派之中新的精神资源之匮乏与心态之偏激”余杰如是说。首先余杰将义士杨佳断然定义为刑事罪犯，同时与怯懦之徒唐永明相提并论本身即荒唐至极，因为两者的精神境界，正义性英勇度完全不在一个层面。杨佳是个货真价实的英雄义士，至于他是否反共，无关紧要；但从他选择七月一日特殊问责，既不伤女警也不碰年青警官，勇于与恶警讨公道，敢于与中共暴力象征和工具的警察局叫阵，“你不给我一个说法，我就给你一个说法！”“我没有罪，有罪的是他们（恶警）！”“警察之所以敢这样作恶，是因为他们背后有你们（伪法官及流氓中共）！”其法庭上铿锵有力的自我抗辩证实杨佳确实堪称反共英雄！至于唐永明，敬请余杰指证是哪位“正义代言人的人士”称之为反共斗士？！<br />
最后，余信口开河地称：“越是远离中国的流亡者，越喜欢为杀人者叫好。在嗜血之外，又多了一层比杀人者更可恨的怯懦和虚伪。打着正义的旗号、唱着道德的高调，为暴力鼓与吹。” 我不知道是否有人为杀人者叫好或嗜血成性。南郭坚称杨佳是令人敬重的英雄义士，我为之强力抗辩是对他的英勇抗暴精神的充分肯定，也是维护其正当合法权利。南郭历来坚决反对滥用暴力，尤其是中共官方的流氓暴力。但南郭坚决支持一切反抗中共专制暴政的正义力量！暴力有正义与革命暴力，岂有只能任流氓暴力横行霸道却不容正义革命武力反抗之理？！民间反抗暴政而施武力抗暴者一则少得可以忽略不计，二则其能量十分有限。令人奇怪的是，余杰不去谴责流氓中共如此众多骇人听闻狗仗人势的暴政的暴力，反而不分青红皂白地指控[暴力]。<br />
<br />
&nbsp; &nbsp;南郭完全理解在专制暴政控制下的国内人士量力自保，其实，余杰与体制内人士一样，根本不敢触及能动摇中共专制暴政基础的要害，只敢在中共允许的范围内作些无关痛痒的批评，人们对此皆能理解，也不会因此而指责其怯懦。然而他实在没有必要装出一副英雄状，指责民运人士，海外人士，不应胡说八道指控他人虚伪怯懦，更不该信口开河任意指责海外人士对中共专制暴政直言不讳的批判。余杰在郭飞雄事件后的辩解那才是货真价实的虚伪，其真实动机冼岩先生的评论是相当到位的，亦即：压住郭飞雄以便确保自已的领袖地位。</div>

							
							
							
							
