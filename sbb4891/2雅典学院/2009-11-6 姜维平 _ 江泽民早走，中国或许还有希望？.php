													<h2>姜维平 :  江泽民早走，中国或许还有希望？</h2>
						
						
																											<div id="postmessage_316320" class="t_msgfont">　自由亚洲电台　2009年11月05日 <br />
<br />
虽然，60年国庆阅兵仪式早已结束，但所谓两代国家领导人站在天安门城楼上共同检阅时的情景，还历历在目，回味无穷，特别是老朽不堪，行将就木的江泽民令人看了，很不舒服，有媒体根据其西服的内衬推测他的健康状况不佳，我认为不无道理，再加上朱g基的一套吊丧的黑西服和墨镜，与一群面无表情的官员簇拥在一起，真是相得益彰，意味深长。<br />
<br />
我不禁想起了中国老诗人臧克家的名句：有些人死了，但他还活着，有些人活着，但他早就死了......现在，展现在我们眼前的不正是这样一副动人的情景吗？不愿用孩子们的鲜血染红中南海地毯的赵紫阳走了，但他还在每一个有良知的中国人心中永远地活着，而步履蹒跚的江泽民呢，还硬挺着站在那里，以显示太子党的后劲和实力，但从&quot;六四惨案&quot;发生那天开始，他的灵魂早已走了！走得连猪狗都不如！于是，我思考一个严峻的现实问题：江泽民早走，中国或许还有希望？<br />
<br />
做为一个中国人，不论持有怎样的观点，只要关心中国的进步，都不得不承认，现在国家正处于十字路口上，不仅中共高层明显出现了分裂，而且社会矛盾也空前激化，正因为是长期的一党执政，手中有权的官员个人品质才尤为重要，在此紧要关头，中南海政治局内某几个人的喜怒哀乐都可能改变事件的走向。毫无疑问，江泽民曾经利用手中的大权，影响了中国历史的进程，我们不妨稍微回顾一下昨天发生的事件。<br />
<br />
据博讯新闻网表示，中国从1989到2009年这20年间发生了两次真正的政变，它都体现了&quot;突然变换执政领导人&quot;的特点，而且，两次政变的主角和最大受益人都是江泽民。我认为这种分析很切合实际。<br />
<br />
第一次是1989年的&quot;六四事件&quot;。当共产党总书记赵紫阳回应人大常委会党组建议，于5月21日电告在美国访问的人大委员长万里立即回国召开人大常委会来缓解学运时，当时在上海主政的江泽民，服从邓小平的秘令，派人在上海的虹桥机场强制软禁了提前回国的人大委员长万里，以阻止他召开人大常委会，防止他作出对人民有利，但对&quot;老人党&quot;和&quot;太子党&quot;不利的决定。<br />
<br />
据报道，&quot;六四&quot;前后的七届人大常委会包括委员长在内的全国人大常委大约有120人，而其中公开签名要求召开人大常委紧急会议，讨论戒严合法性问题的常委多达57名，已达到法定人数的五分之二。万里从一开始就肯定学生运动的爱国热情，并像赵紫阳一样主张在民主与法制的轨道上解决学生提出的促进民主和惩治腐败等问题。因此，一旦全国人大常委开会，必将否决李鹏姚依林等人主张军队在北京实行戒严决定的合法性。按照宪法规定，全国人大是&quot;最高权力机关&quot;，一旦人大常委开会作出否定的决议，不仅李鹏姚依林等政治局常委主张镇压学生运动的阴谋落空，而且他们也必将辞职下台。<br />
<br />
如同今年的&quot;十一阅兵&quot;，只要打开记忆的闸门，往事就会波飞浪涌。我记得万里委员长走下飞机时，人们从电视上没有看到往常按惯例出现党政领导人列队欢迎的场面，他不仅被剥夺了在机场发表支持学运的谈话的权力，而且莫名其妙地穿上了宽大的病号服，被江泽民胁迫而去，从此他不得不默许与屈从他们在北京实行戒严，镇压学生运动的非法行为，这一镜头成为一出震惊世界的六四屠杀悲剧的序幕。<br />
<br />
当然，在党的总书记赵紫阳下台被软禁的同时，江泽民因胁迫万里&quot;疗养&quot;立下大功，进而取代赵而成为新的党总书记，因此在&quot;六四政变&quot;的幕后操纵者邓小平的扶植下，江泽民成了最大的受益人和赢家。<br />
<br />
中国历史就是如此荒诞而令人悲哀！六四的民主曙光照亮了东欧，改变了前苏联，但把漫无边际的共党专制与黑暗的长夜留给了中国人民。有志之士不满足于经济发展，国富民穷，还需要公平，正义，法治，民主和人权，所以不断地发出声音，也不断地入狱，逃亡，牺牲。于是，有人认为胡温未能走出江泽民的阴影，希望老态龙钟的江泽民早点离去，包括党内的一些改革派也莫不如此。<br />
<br />
大家回想一下，在召开中共十六大之前，十五届中央政治局常委早已达成共识：年龄上限是70岁，包括当时年仅68岁的李瑞环本人，也愿意提前两年陪同76岁的江泽民一起全退。他当然无话可说。但江泽民及其亲信曾庆红阳奉阴违，搞突然袭击，利用军委副主席张万年串联了十六大主席团里20多位将军，提出&quot;临时动议&quot;，要江泽民留任16届中央军委主席一职，用这一&quot;特别动议&quot;否决了政治局常委会、政治局五次会议做出的决议。他们还逼迫胡锦涛当场表示顺从。同时，江泽民又把几个亲信当成私货，硬塞进16大政治局常委成为多数。这样，江泽民就可以继续把持军委，操控政治局常委，主掌国家最高权力，也就是使胡锦涛变成如同光绪皇帝一样的傀儡和&quot;儿皇帝&quot;。<br />
<br />
博讯网的文章说，这是又一次&quot;准军事政变&quot;。中共20年来的第一次政变，江泽民窃取了赵紫阳的权力，第二次政变，江泽民笼罩了胡锦涛的权势。我认同这种观点，他充分说明了江泽民贪恋权力，拉帮结伙，误国害民，延缓党内民主进程，何其重也！还是原纽约时报驻北京记者，《中国觉醒了》一书作者纪思道分析的深刻，他认为中国有三个谎言，其中后两个谎言即是谈这个问题。他说，胡温都不过是&quot;跟班&quot;，江泽民仍然具有影响力，今年十一阅兵时，江泽民还站在人们的视线中，足以证明纪思道的前瞻性。<br />
<br />
江泽民的确是一个人品恶劣的江湖政客。2007年我应日本《读卖新闻》驻上海支局长加藤隆则之邀，为筹备首次赴日个人书法作品展，曾先后两次小居沪上，外出走动，均以出租代步，我询问了23个出租司机，大家对江泽民评价最低，他连陈良宇都不如，只有一个人说，他盖了很多大楼，还不错。其中一个中年男子对其评价最形象深刻：江泽民是一个跑江湖的大骗子，&quot;六四&quot;别人摔倒了，他捡了一个大皮夹，里面的钱全归他的啦！他也厚脸皮，不害臊！。。。。。。是啊，就是这样一个上海&quot;江湖骗子&quot;统治了中国。<br />
<br />
后来中国人民总算熬到了17大，江的亲信曾庆红引退的代价，不仅是安排三个江泽民得意的人挤进了政治局常委，而且还提名太子党习近平为&quot;储君&quot;，显然其目的是在18大胡锦涛退休后，党和国家的最高权力完全落在江泽民控制的党羽手里。习近平最近在德国向默克尔赠送江的著作已透露了玄机。而眼下薄熙来的唱红打黑表演和在17届4中全会上的躁动不安与&quot;党内民主&quot;的诉求，也无一不是江泽民，李鹏为首的党内保守派精心策划的一次&quot;准军事政变&quot;，其目地都是枪班夺权。我想，他所瞄准的位置至少是国务院总理吧！<br />
<br />
近期有报道说，在党的17届4中上全会上，胡锦涛利用房司令以武力阻止习近平任中央军委副主席，也粉碎了薄熙来的黄粱美梦，但消息未加证实。不论怎样，接班人的问题，总要有一个制度性结果。值此之时，有人又说，江泽民早走，或许中国还有希望！我认为，问题并不如此简单，即便江泽民走了，但一党执政的专治制度不变，新上任的领导人又会贪恋权力，另立山头，还不知道会玩出什么新花样来。当然相互比较一下，同是一个党派，李克强，温家宝确比其他人要开明一些，实干一些，温和一些，假如中国能够出现一个象戈尔巴乔夫式的人物，就会事倍功半，少走许多弯路，因此，国内的维权运动和海外民运与中共党内改革派的对接特别重要。<br />
<br />
从目前胡锦涛对习近平和薄熙来的态度看，他正在带领共青团系人马拒绝江泽民隔代指定自己的接班人，试图用&quot;党内民主&quot;的方式进行微妙的反抗，这可能一方面符合政治局常委和中央军委成员多数效忠于江泽民的实际情况，另一方面也基于胡锦涛优柔寡断，僵化呆板的性格特点，但胡的城府很深，也有深不可测随机应变的可能性，假如江泽民早点走，他能学习赫鲁晓夫，或许还有顺应民意思维突变的空间。当年，前苏联的政治局委员多数倾向斯大林的接班人马林柯夫，赫鲁晓夫就是连夜派专机从全国各地把倾向自己的中央委员接到莫斯科，紧急召开了中央全会，用表决的多数选票战败了马林柯夫。细想一下，中共十七大共有中央委员204人，候补中央委员l66人，其中有多少人倾向于江或胡？胡锦涛及其智囊们应当有一个果断明智的决策！特别重要的是，他们还应当在初步成功之后，大举改革，不仅要使党内派别斗争公开化与合法化，而且还要开放党禁和报禁，平反六四和法轮功，实行新闻自由和司法独立，开启民主进程，实现宪政民主，把中国建成一个既富有又文明的国家。<br />
<br />
不过，这正如我本人也希望江泽民早走一样，只是一个书生的海外奇谈，还要看天意。其实不论他早走晚走，反正总要走，走了，地球就会转动快些，同样不论哪个派别上台，制度依旧，孰胜孰败，均非人民之福，也是真理。但我坚信，总有一天，中国会拥有一个赞成普世价值，人权法制的社会！<br />
<br />
2009年11月5日于多伦多</div>

							
							
							
							
