首先，政治是披着诡辩外衣的利益争夺。<br />
其次，文学是一面折射百态人生的多棱镜，我觉得文学的主要任务就是揭示现实生活中人性的蒙昧，展示理想化的道德风尚，并关注宇宙进程的神秘奥妙。<br />
那么，政治就是窥视文学所提供的某些概念，用来装扮自己。<br />
