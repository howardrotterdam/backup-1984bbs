<br />
在当下中国，谈民主宪政和谈共产主义是同样遥不可及的幻想。<br />
普通民众的觉醒需要的是不断反复的启蒙运动！<br />
什么是启蒙？启蒙这个词就很能说明问题。简单的概括一下，就是启发人认识到人性的蒙昧。<br />
人生是惨淡的，人性是蒙昧的，参照西方中世纪的启蒙运动，十日谈、堂吉诃德等等一系列直指人性阴暗、蒙昧、混沌无知的文艺作品痛揭了宗教和神学掩饰在人类身上的遮羞布。<br />
当时的欧洲，在文学领域内是什么样的状况呢？希腊语、拉丁语等宫廷化、贵族化的语言逐渐地被德语、法语、英语等撰写的或是翻译的作品所替代。<br />
在西方的中世纪之前，文学作品所描述的特定型态的人性大多数仅限于贵族和英雄人物的兴衰历程。<br />
而到了启蒙时代，就直指普通平民的尴尬处境和更普遍意义上的人性蒙昧现状。<br />
这种写作传统，在之后被雨果、巴尔扎克、哈代、狄更斯等人继续发扬和传承了下来。<br />
<br />
而反观中国，自文言文，一种类似西方的官方贵族语言逐渐被“引车贩浆之徒”所操持的平民语言口语化之后，在上个世纪的二、三十年代掀起了一股启蒙运动的热潮。<br />
很不幸的是，这种平民生活写照的白话文在政治的淫威下被赤化、官方化，甚至被神化。<br />
群体性的语言被单一化，固定化。<br />
普通民众由于对那种特定语言、特定偶像的崇拜而通过它将自己升华，接近神或是达到神的高度。<br />
经历过文革场面的人想必对这种感觉有所体验。<br />
中国的当下需要的是什么？需要类似堂吉诃德、阿Q、狂人日记一类揭示民众普遍蒙昧无知现状的文学作品。<br />
中国需要狄更斯、巴尔扎克式的作家！<br />
