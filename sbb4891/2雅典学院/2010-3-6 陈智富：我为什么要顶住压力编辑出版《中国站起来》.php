													<h2>陈智富：我为什么要顶住压力编辑出版《中国站起来》</h2>
						
						
																											<div id="postmessage_504935" class="t_msgfont">我为什么要顶住压力编辑出版《中国站起来》<br />
<br />
陈智富（《中国站起来》的责任编辑）<br />
<br />
 <br />
<br />
摩罗先生沉寂十几年，在2010年元月出版新作《中国站起来》，结果招致了很多批评，引发了极大争议，在网络上闹得沸沸扬扬，众多名家纷纷登场发表声音。人们对摩罗的思想转变非常不理解，特别是一些自由主义知识分子试图以沉默来表达愤慨和不屑。可是，那些真正的洋奴都躲得远远的，笑看爱民族的自由派和爱民族的摩罗新左派内讧、互掐。<br />
<br />
作为《中国站起来》的责任编辑，我也招受了空前（对我自己而言是空前的）的批评。有人不仅在不看书的情况下，要向《中国站起来》泼脏水，要给摩罗贴上“阿Q或者祥林嫂的儿子”的新绰号，而且要以“忽悠的小编辑”的称呼表达对我的蔑视。这一个多月来，我的不少朋友都在问我，你为什么要出版这样的书？颇有痛心不已的心情。仿佛我从一个有为青年，瞬间沾上了民族主义的毒草，就堕落成不良少年了。我的本家陈赋兄第一时间撰文反对《中国站起来》，近日问我：“以老兄之思想，出版摩罗之书，在下很是好奇，你真的同意他的观点？”他们的关切之情，是出于真诚，并非恩怨。我十分理解，回答朋友的疑问次数多了，我觉得，还是有必要站出来说几句。<br />
<br />
 <br />
<br />
首先，出版应该是中立的，应该朝着有益社会的方向努力。责任编辑应该有担当社会责任的勇气和识见。我一向主持理性平和探讨问题，报纸和出版都应该是社会公器，不能成为一家之言的独唱团。我觉得，民族主义情绪本身并没有错，摩罗从一个自由主义者转变到新左派阵营，具有一定代表意义。这个问题值得我们知识界思考，特别是值得当代中国青年思考。所以，我愿意出版摩罗新作《中国站起来》，希望我们的知识界和青年能够进行更深入的探讨，寻求民族发展的道路和方向。这便是我的初衷。<br />
<br />
自由派看到摩罗思想左转，就首先扣上了一顶“被招安”的大帽子，其实这是误解。摩罗新作《中国站起来》出版并不容易，我历经磨难，做了大量的修改，勉强通过了出版审查要求，才一不小心出版的。另外，这并不是自由派所臆想的那样“摩罗一旦华丽转身便黄金万两”。如果摩罗想被招安，几年前就可以了，何必等到受了那么多苦主动被招安呢？在D看来，新左派更危险。（以下文字和谐）<br />
<br />
当然，不管怎么说，报纸和出版都不可避免地会带有一定的倾向性。物以类聚，人以群分。没有起码的认同，也不可能去一起做一件事情。但是，没有起码的不失尊严的距离感，也不可能共同把一件事情做好。像钱文忠一样，我赞同摩罗新作《中国站起来》的部分观点，但不是全部。任何一本书都会有优有劣，总有人说褒贬，这不足为奇。摩罗的恩师钱理群先生就说，虽然自己不是完全赞同《中国站起来》，但是觉得以这本书为蓝本，促使知识界对当代中国的思想进行讨论，则是很有必要的。<br />
<br />
关于《中国站起来》的各色言论，我基本都收集了。名家言论看的多了，我这里引用两则普通网友的正反评价：<br />
<br />
有网友说：“我读《中国站起来》的感觉和以前读过希特勒的《我的奋斗》的感觉差不多”，用叶匡政评论的话来说，感受到的是“敌意和荒唐”。”<br />
<br />
有网友发文支持摩罗，也在对叛徒的定义表达疑惑。以前，我们常把那些卖国贼当成叛徒，加以抨击。如今，从何新，到《中国不高兴》的作者，到摩罗，怎么都成了叛徒呢？在中国的大地上谈论如何爱中国，居然会被批判为叛徒。真是可笑。（大意）<br />
<br />
如果《中国站起来》真的是一无是处，那么，我们又怎么解释有那么多青年认同呢？起码，原本对所印图书很麻木的印刷厂的工人都跟我说，《中国站起来》是一本好书。（另外专文介绍）。凡事无绝对，凡人别偏激。如果有人一棍子打死一本书，或者有人一棍子捧杀一本书，这种言行本身就是可疑的。<br />
<br />
如果在自己的国家爱国都是一种罪恶的话，那么我们还能爱谁的国家呢？我以为，反对不公的制度，可以；反对摩罗爱国，就不对。政权可如走马灯，你方唱罢我登场，王朝更迭是人类社会必然的新陈代谢，我们可以反对那些十恶不赦的腐朽政权，但是不能因此反对自己的民族和国家。我们不能因为有中国人表达了一点民族主义情绪，就给人家扣上法西斯的帽子，更不能对别的外国人高涨的民族主义情绪熟视无睹。反对中国贪官腐败，是对的；难道反对西方贪官腐败，就错了吗？或者说，难道不应该反对西方贪官腐败吗？这种区别，某某人是否意识到了呢？<br />
<br />
前几天，我在博客里转帖了《宋鲁郑：中美冲突，自由派何以集体失声? 》、《张宏良：美方公布的“中美国”文化别动队名单――评美国评选的“中国百大公共知识分子”》，分别加了一段谈左派右派的按语，不久就被和谐得无影无踪。莫名其妙的是，我的微薄也无法发帖了，只能转发别人的帖子。看样子，东厂进化了，可喜可贺啊。后来，我写了《当代中国青年思想正在左转》的文章，只能在乌有之乡和毛泽东旗帜网才能不被和谐掉。这些谈青年思想、左派、右派的文章，能够更好地表达我的思想倾向，但是没办法贴出来了。（以下文字和谐）<br />
<br />
 <br />
<br />
其次，这一切批评的局面，我和摩罗之前都有预料到，但是没有想到如此猛烈，猛烈地让人窒息，以至于我不敢相信，自由派所呼吁的言论自由原来就是这样子的，原来就是这样几乎一边倒的合谋，原来就是欲除之而后快就丝毫不掩饰扼杀于襁褓之中的私心。<br />
<br />
这波反对《中国站起来》的浪潮，以易中天第一时间抨击为开端，以近日出炉的2009年烂书榜为标志，进入了第一个小高潮。我的好友、《中国不高兴》的作者之一宋强在第一时间告诉我：“其实他们是想对不高兴一伙儿和摩罗发难，他们就是想搞死《中国站起来》，其他几本书都是做陪衬的。”诚然，2010年新书被评为2009年烂书榜第二名，只是，诸如郎咸平等人的大腕太大牌，恐怕也不甘心为摩罗做陪衬的吧。我尽管非常不理解，但也没有特别在意此事。毕竟这个烂书榜并不权威公正，也不客观科学，不具备什么说服力。宋强兄为了鼓励我，过年特意发了一条祝福短信：“黑居易，居大易！挺摩罗，助诗力！神州复兴奔向胜利。”<br />
<br />
前几天，陈希我、十年砍柴，两位2009年烂书榜的评委跟我聊起摩罗《中国站起来》。（《长江商报》周日的文化副刊，将刊登陈希我抨击《中国站起来》的文章）他们只是希望从学理等方面指出《中国站起来》的欠缺，希望更理性客观地评价当前的民族主义思潮。陈希我前几天写了篇博文《谁有资格对中国发言？》，开头就写道：“接到一个熟人转来的一篇文章，对我及一些人评《中国不高兴》、《中国站起来》等为2009年“烂书”大为不满。”莫非他说的就是我写的那篇《2010年新书被评为2009年烂书，怪哉！》。对他们说的很多观点，我都是比较赞同的。包括陈希我批摩罗为爱国疯子那篇文章，在新浪、网易等广为传播，我对其中的一些观点也是赞同的。他们其实都是我很好的朋友，我从他们身上能学到很多知识，受益良多。他们从自己的角度出发，探讨民族发展之道，并不属于摩罗批判的对象。<br />
<br />
可是，为什么他们也要坚决批判摩罗呢？这让我想起了鲁迅的《阿Q正传》。毋庸置疑，鲁迅的《阿Q正传》是一部具有划时代意义的巨著，虽然篇幅不长，但思想意义不同寻常，至今仍有顽强的现实生命力。出版之初，鲁迅就遭遇了巨大的批评，很多读者都喜欢对号入座，认为自己就是鲁迅所嘲弄的阿Q，觉得自己背鲁迅骂了，于是就愤愤不平起来。其实，大可不必，鲁迅虽然信奉睚眦必报，但压根儿就不是那样小肚鸡肠的人。现如今，很多自由派的知识分子觉得自己被摩罗骂了，心里于是就不爽了。其实，摩罗对自由主义分子都清晰的认识，不是一棍子打死，而是区别对待。对那些爱民族、反专政的自由主义分子，他是很欣赏的。他所反对的是那些“身在中国、心在西方”的洋奴，就是那些买办卖国右派。就是如陈志武、茅于轼之流。摩罗曾经跟我讲过多次：“中国当代知识分子大致有两派，一派是关注国内人权的，一派是关注国家族权的。这两派知识分子都认为自己的关注问题是最急切的最紧迫的唯一的焦点问题，而否定另外一类。其实，这两派知识分子应该求同存异，实现合流。”所以，他一再强调，既要站在弱势群体的角度向国家强权挑战，也要站在弱势国家的角度向霸权国家挑战，两者缺一不可。<br />
<br />
另外，在中国失声多年的余杰，在第一时间宣布与摩罗绝交后，近日撰写长篇檄文《谁动了中国人的奶酪？――从《中国可以说不》、《中国不高兴》、《中国站起来》之“三部曲”看中国的法西斯思潮》，抨击摩罗的《中国站起来》。很多人都说，摩罗早已不是曾经的摩罗了，余杰又何尝是我们敬佩的风华正茂的文学才子呢？其实，我很清楚，余杰为什么那么愤怒，因为《中国站起来》抨击“有人”呼喊西方再来殖民中国三百年。这个“有人”就是余杰之流。不过，我想说的是，余杰们，胡适先生曾说“宽容比自由更重要”，知否？余杰们，你们可否在反对摩罗爱国的同时，也反对洋奴卖国呢？余杰们，可否在您抨击国内专制不人道的同时，也能抽出一点闲暇对美国霸权表示抗议呢？我并不否认国内的种种丑恶，我并不否认执政党的昏暗，但是我同样不能忽视美国对台军售、C形包围中国等等迫在眉睫的国际局势。知否，知否？<br />
<br />
关于《中国站起来》，关于摩罗，关于左派，关于右派，关于民族主义，关于中国青年，关于太多的东西，真的是一言难尽，任何一言都难免片面，难免授人以柄，奈何奈何！<br />
<br />
2010.2.26 草成</div>

							
							
							
							
