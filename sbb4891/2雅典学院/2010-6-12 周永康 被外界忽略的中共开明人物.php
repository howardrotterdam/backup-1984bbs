													<h2>周永康，被外界忽略的中共开明人物</h2>
						
						
																											<div id="postmessage_681517" class="t_msgfont"><a href="http://news.boxun.com/news/gb/pubvp/2010/06/201006112122.shtml" target="_blank">http://news.boxun.com/news/gb/pubvp/2010/06/201006112122.shtml</a><br />
&nbsp; &nbsp; 匿名作者<br />
&nbsp; &nbsp;&nbsp;&nbsp;2012年将是中共换届的时刻，政治局党委将有7人退出，退出的7人中除了胡温会被历史看好外，周永康将是第三位最终会被历史看好的人物，这一点可能有人不赞同，笔者由于和大陆法院、司法部门打过交道，把自己了解到的事情讲出来，一方面是对一个人物的公正看法，另一方面是对今后十八大上位的中共核心领导人提供一个可以借鉴的执政方式。<br />
&nbsp; &nbsp;&nbsp;&nbsp;(博讯 boxun.com)<br />
<br />
胡温周巧妙避免了重大事件发生<br />
<br />
&nbsp; &nbsp; 中共在1949年夺得政权后，每十年的后半叶都会出现政治大动荡，50年代的57年反右运动，几十万知识分子被打成右派；60年代的66年文革开始，一直延续到76年；80年代的“8964”惨案；90年代的法轮功；共产党执政形成了每过十年都有一次席卷全中国的重大事件发生，每一次不仅毁掉了一代人，而且还给中共执政带来巨大的冲击。以上归其原因，是由于中共在执政过程中出现了重大失误，邓小平改革开放，的确很伟大，但也没能避免“8964”的流血事件；江泽民可以说是中国历史上最懂经济和建设的领导人，但也没能处理好“法轮功”事件。历史进人到21世纪，特别是05年到09年能否再次发生类似 “8964”和法轮功的事件，能否避免又给中华民族带来巨大的伤痛，后来的中共领导人吸取了历史教训，在执政方式上发生了很大变化，胡温亲民执政、疏导民意外，周永康推行的一系列政法新观念和做法，切实保障了亲民政策的推行，碰了很多敏感问题，打破了很多禁区。周认为过去公检法、司法和信访系统像大爷，脱离老百姓，出了问题往上推，周主持工作后，把这些大爷赶出去面对社会问题和底层老百姓，化解矛盾、疏通民意，消除民怨。2012年中共十八大到2022年中共二十大，以习近平、李克强的智慧，如果能够再一次避免出现类似8964事件或者法轮功事件，在保证共产党统治的前提下，习近平、李克强等人极有可能在十年的后半叶像邓小平出山后那样为历史冤案平反，甚至是64惨案，极有可能比照平反57年右派的模式，并留有尾巴。目前胡温周所做的一切可以说是一个很好的为今后习李有所作为的铺垫。<br />
&nbsp; &nbsp; <br />
基层把周比喻成历史上的董必武<br />
<br />
&nbsp; &nbsp; 笔者08年在从贵阳到湖南的火车上，遇到一位湖南某县检察院院长，聊天时表示：最近他们在学习周永康的一个长篇讲话，其核心内容是观念转变，即法院检察的独立性，程序过程，等等，情不自禁说，周讲话非常清新，很有思想，就像历史上的董必武。周永康阅历丰富，从基层干上来的，曾在中国石油任总经理，懂经济，在国家机关和地方都干过，处理问题不死板，比较开明和公正，甚至敢闯一些敏感区域，我们现在看见媒体的自由度比过去有所进步，政府官员不像过去那样对新闻媒体横加指责，这一改变是从孙志刚事件发生后开始的，周永康7次亲自过问此事，最后取消了不得人心的收容制度。高耀洁到美国，先是美国希拉里带话给胡锦涛，这种美国、英国等政要很多向中共领导人提出各种要求，成功率非常小。河南的朋友告诉我，当地基层政府表示决不能让高到美国，找个理由打个报告上去就可以了，如果是过去的其他人肯定是相信基层，以任何一个理由就可以拒绝希拉里的请求，但这次遇到了周永康，由于周的坚决，高耀洁才可以到美国和再次去美国。大陆的民间对日索赔和保钓是地道的草根运动，，日本政府从清朝以来对中国的政策是，利用中国政府对付中国老百姓，无论与中国老百姓和中国企业发生冲突，首先向政府外交部施压，大陆的外交政府官员以中日友好的理由来处理问题。当周永康主政公安部后，不是采取一刀切的办法来对待大陆的草根运动，对大陆民间对日索赔和保钓采取了默许的办法，没有按照传统的做法，大陆才迎来了保钓和索赔的春天，大陆保钓人士登上钓鱼岛，对日索赔也有所结果，大陆的保钓领袖和对日索赔领袖也深有同感。<br />
&nbsp; &nbsp; <br />
大陆社会进步的重要推手<br />
<br />
&nbsp; &nbsp; 周永康与曾庆红可以说有些相似，有大气魄和雄才，敢作敢为，不拘泥传统模式，但在大陆这种复杂的社会很难有大的作为，再由于大陆的干部体制弊端，没有上级的指示，下级不会碰敏感问题，没想到近来出现的重庆公安局局长王立军和北京公安局局长付政华，这两人有一定的抱负，正好符合周的胃口，王立军周端掉文强；付正华揭开了“天上人间”的神话，这在过去是不可能的。“天上人间”之所以有这么强的生命力，一直是披着“引进外资、照顾外国朋友的生活习惯”的合法外衣，而保留的最豪华的“人肉市场”。大陆各地也纷纷效仿，在当地的星级酒店设立“天上人间”，当地的政府官员以陪同外国老板的名义经常光顾本地的 “天上人间”。这实际上是一条不成文的政策底线，搞得不好，会戴上破坏“招商引资”的帽子。如果大陆出现更多的王立军和付正华，这正是周目前所需要的。律师协会的选举，说明周永康有浓厚的民主色彩。据律师协会的朋友透露，现任大陆律协主席于林是地地道道选举的，如果是推荐，肯定是另一位高干子弟当上律协主席，周当时任政法委第一副书记，首肯了律协的选举结果，这在当时是罕见的，因周当时还不是常委。周永康对律师也很关注，当上常委后没几天就见了律师队伍，对台湾法律专家谢启大的关于嫌疑人尽快找律师的建议很重视，谢启大曾当过台湾新党领袖，到大陆后致力改变大陆的司法弊端，大陆犯罪嫌疑人被关后，很久不让请律师，谢启大感到这一做法不符合法理，在大陆走到哪就批判到哪，现在大陆犯罪嫌疑人找律师比以前自由得多已成为现实。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 我所听到的事情很多，一时也想不起来了，对大陆任何一个人评价，应该实事求是，做的好的，应该支持，做的不好的应该批判。 [博讯首发,转载请注明出处]<br />
