													<h2>孔庆东：20年前我指挥5万大学生，举行全市总游行</h2>
						
						
																											<div id="postmessage_82848" class="t_msgfont">今天是个特殊的日子，先说几句往事。<br />
<br />
20年前的今天，我和其他一些血气方刚的同学，指挥5万北京的大学生，举行全市总游行，史称“4・27大游行”。天亮前向各兄弟院校发出庆东一号手令，早上在北大五四操场誓师出发。一路汇集清华、人大、北外、民院、北钢、北航、北邮、北师大等各路友军，高唱《国际歌》、《三大纪律八项注意》和《一无所有》、《少年壮志不言愁》等劲歌，连续冲破三道军警人墙，洪流浩荡，直扑天安门广场。沿途百万市民箪食壶浆，欢呼呐喊，“打倒腐败”和“振兴中华”之声直上九霄。男女老幼皆动情，军民官商齐落泪，白云为之屏息，苍天为之瞠目。至中午时分，英明的政府宣布此日游行为爱国行动，同意与学生代表进行对话协商，撤去广场伏兵，送来面包汽水，并派警察协助维持秩序。一时“共产党万岁”之声响遏行云。五万同学，经建国门各归本校。孔和尚率亲兵四百，护卫北大校旗，于深夜最后一批返回燕园。自海淀黄庄至北大南门，欢迎队伍人山人海，镁光灯闪闪不绝，如节日焰火。甫入南门，突有一圆头罗汉上前扑入庆东之怀，二人相拥，热泪滚滚。庆东收泪抬眼看去，23楼垂下一条巨幅白幔，上书：“后生学先生，先生赶后生，生生不已”。此乃建国后第一次完全胜利的大规模学生爱国游行也。<br />
<br />
20年后的今天，下午去北大开了两个会，内容恕不透露。晚上特意去吃了涮羊肉，以缅怀往事。当年北大最高级的美食，就是学四食堂的涮羊肉也。收到美国老同学的来信：“老孔，就在两天前，春日高照，我的美国学生还在课堂上天真地问我，二十年前的今天老师在哪里？我说，我现在就可以给你们脑子里面放电影，比一切纪录片都真。记得我们走在去三角地，开始那关键性的一切的路上，也是这样阳光暖洋洋。我们说，也许五四也是这样开始的。记得26号晚上在三教围绕游行还是不游行的激烈辩论，记得27号的一整天顶风大游行。你深夜回来时……二十年后，我们都还在。我们的心都还在一起，真是温暖。没有比人心更珍贵的了，跨洋握手。大家都好好的！”<br />
<br />
 回复了一封四川来京打工者高勇的来信。很多打工者颇有文化水平，如果工作顺心，待遇公平，他们就是和谐社会的建设者，是中华民族的主人公。<br />
<br />
山大华特卧龙学校的高国良同学，收到了你的《镇天诗稿》，你的文字功底不错，字里行间更透出雄心壮志，中学生能达到这样的水平，难怪你的同学们要称赞你了，我看至少比我上初中时写得好。希望你先听毛主席的话：谦虚谨慎，戒骄戒躁；再听孔老师的话：广种薄收，多劳少得。<br />
<br />
《羊城晚报》的“七天精摘”版，09年3月22日起连载孔庆东《十大元帅的情缘》，选登了其中的前七篇。看来广州目前还是共产党的天下，文化界对革命历史至少仍怀着人性的同情。有人说广东地区的媒体已经完全操控于汉奸卖国贼之手，此话说得太过激了。其实一百多年前，广东就是中国革命的火车头。从康有为梁启超到孙中山叶剑英，广东一直是走在时代前列的。该报的连载，是唯一保留了原文后面的“思考题”的一家，从专业角度看，编辑的水平也是相当高的。<br />
<br />
日前收到杜晓勤《齐梁诗歌向盛唐诗歌的嬗变》，北京大学出版社09年3月版。这是杜晓勤博士论文的修订版，葛晓音老师在序里给予了很高的评价，并顺便批评了那种盲目追新逐异的不良学风。对我来说，更感兴趣的是近体诗的格律究竟怎样形成的，蜂腰鹤膝等病究竟怎样在文学史的发展中被诊断和宣判的。书中的统计表以具体的数字有力地说明了这方面的问题，并体现出作者的辛劳。现在有更多的青年人写作旧体诗了，一般粗通格律就沾沾自喜，不明白为什么要有格律，还有什么是高雅的格律。那种就知道“天对地，雨对风，大酱对小葱”的浅薄者比比皆是。不明白或者不承认传统意义上的“文学”乃宇宙间最高学问者，终不得窥学术之堂奥也。<br />
<br />
《中国青年报》09年4月7日“阅读记忆”登载《孔庆东：什么是爷爷辈的书，什么是孙子辈的书，要一眼分清楚》。题目这么长，大概是为了吸引读者注意。团中央的机关报现在都这么搞了，孔和尚很担心，将来有一天，《人民日报》的头版新闻是：《我不想说我不愿说可我不得不说否则就得憋死屈死憋屈死――党的十八大政治报告全文》。<br />
<br />
记得20年前的这个时节，我两周没去上课了，心中有些忐忑。一天抽空跑去阶梯教室，刚进后门，就见一位圆头罗汉朗声说道：“中国的知识分子，平时要严谨扎实做学问，甘于世俗，甘于平淡。而一旦国家有事，便要挺身而出！”听到此，我心豁然开朗，一转身，便大步走了出去。<br />
<br />
外面，满天热风，刮得正紧。<br />
