&nbsp; &nbsp; 我所在的连队，在南方某山区，现役士兵十之八九都有沾亲带故的社会背景，许多人当兵还花了不菲的一笔钱，目的就是为了在部队入党提干、上军校镀金，或者日后转业到地方能找社会关系安排个好工作。我是农家子弟，刚入伍时还有股子青春热血，盼望有朝一日能为祖国为人民冲锋陷阵，实现自己的人生价值。但是后来，由于偶然机会，我耳闻目睹部队的贪污腐败事实，亲眼看到班排长、连长、营长、团长、师长和大小参谋们一个比一个讲究吃穿排场，一个比一个腐化堕落，我的心在默默流血，以前的理想支柱轰然倒塌。现在，连队士兵们都在议论南海、东海、黄海军事，都知道朝鲜半岛终有一战，许多人舍不得老爸老妈好背景、老婆孩子热炕头，开始想办法托关系提前转业、复员或者退伍了。我心已死，也不想给腐败政府当炮灰，也准备趁早打报告退伍回乡，找个出租车司机之类的轻快活儿，挣钱娶妻成家立业，得过且过、了此一生罢了。<br />
<br />
&nbsp; &nbsp; 亲爱的中华网友，最近今天我在中华网论坛上读了不少议论中国与美、日、韩、俄等国会否发生冲突战事的帖子，也认真看了许多热血网友的留言，知道你们打心眼里爱着人民子弟兵，相信他们在关键时刻仍能担当保家卫国的钢铁长城。我感到自己脸上火辣辣的，我承认自己不是个好士兵，可是我原本并不是孬种。国家如此，社会如此，军营如此，谁也难有回天之力了。有不少部队官兵悄悄泡妞、搞钱、玩潇洒的丑事，我实在不好意思说给大家听。我只能把自己当兵以来所看到的真相大体告诉你们，今天军队里面的腐败绝不会亚于社会官场，人民子弟兵再也不是毛泽东时代的队伍了。<br />
<br />
&nbsp; &nbsp; 我没有亲身体验过毛主席时代的人和事，但我从小听爷爷、一个亲老舅、一个远房姑父讲过许多有关解放前和解放后相比照的感人故事，也听过学校在组织清明节祭扫和革命传统教育活动时请老红军、老革命和抗美援朝战斗英雄作的报告，我相信他们都是很好很厚道的人，我敬重他们热爱毛主席、热爱新中国的感情，所以我打小就对毛主席和革命先烈有朦朦胧胧的好感。我到部队后一直带着一本袖珍本的毛泽东选集，那是亲老舅家里保存好多年、我好不容易磨来的红宝书，我大多时候把它藏在箱子里，因为身边战友赶时尚，老笑话我是老土木瓜脑袋，但我并不以为然，我还是经常在安静时拿出来翻一翻。我最爱看的是红宝书中的军事斗争文章。毛主席讲过的两段话，我一辈子都不会忘记。一段话是“这个军队具有一往无前的精神，它要压倒一切敌人，而决不被敌人所屈服，不论在任何艰难困苦的场合，只要还有一个人，这个人就要继续战斗下去”，我想现在我们部队最欠缺的可能就是一往无前的拼命精神，高精尖现代化武器固然也重要，但是没有精神振奋、斗志昂扬的指战员去运用它，再厉害的武器、再多的部队也不过是行尸走肉，我始终坚信军队精神、战斗士气要比军队新式武器重要一千倍。毛主席的另一段话是，“这个军队之所以有力量，是因为所有参加这个军队的人，都具有自觉的纪律；他们不是为着少数人的或狭隘集团的私利，而是为着广大人民群众的利益，为着全民族的利益，而结合，而战斗的。紧紧地和中国人民站在一起，全心全意地为中国人民服务，就是这个军队的唯一的宗旨”，以前每当我想到话中的深刻道理，比比自己看到的部队的事实，我就忍不住要掉眼泪。但是现在我心已木然了，和其他战友一样随大流了，难得为报纸上宣传的感动的人和事掉一次眼泪。<br />
