													<h2>政府和军队腐败奢华程度超过任何一个发达国家</h2>
						
						
																											<div id="postmessage_726894" class="t_msgfont">张召忠将军：中国政府和军队奢华程度超过任何一个发达国家&nbsp;&nbsp;转贴<br />
记者：据报道中国现有公务用车六百多万辆，国家每年光养车的费用就比军费还高，是这样吗？<br />
&nbsp; &nbsp;&nbsp;&nbsp;　　<br />
张召忠：中国是一个发展中国家，但超过世界上任何一个发达国家，真是触目惊心啊！这主要表现在三个方面：<br />
&nbsp; &nbsp; 　　<br />
&nbsp; &nbsp;&nbsp; &nbsp; 一是吃饭喝酒，大摆排场。美国人均GDP是中国的十倍，可美国人公款请客不会超过四菜一汤，标准非常清楚，没有弹性空间，你愿意八菜一汤可以，对不起，多 余的部分你必须自掏腰包！美国人请我吃饭，每次总是把菜汤倒在米饭里吃掉，不浪费一粒米、一根菜，一口汤，我们的官员能够做到吗？一顿饭能够吃掉一个大学 生四年的学费，一个小学一年的经费，这样下去得了吗？当然，在经济萧条的今天，大吃大喝或许也是拉动内需的一个方面呢！你看现在经济危机影响到餐饮业了 吗？相反，却越来越红火。还有送礼的问题，托人办个事儿动辄就是几万、十几万的送，多么吓人！如果有机制，有考核，还需要这样吗？按程序办就是了，为什么 留有那么多的黑洞和暗箱操作空间？在美国，送礼可以，但必须限制在10美元以下，如果超出这个范围，就会涉嫌行贿受贿，受贿的要判刑，行贿的也要判刑。美 国是一个法治社会，它虽然在衰落，但值得我们学习的实在太多了。<br />
&nbsp; &nbsp;&nbsp;&nbsp;　　<br />
&nbsp; &nbsp;&nbsp;&nbsp;　　<br />
&nbsp; &nbsp;&nbsp; &nbsp; 二是楼堂馆所，极尽奢华。我去过很多国家，发现中国各级政府的楼堂馆所比世界上任何一个国家都奢华、豪华、壮观。在美国、在欧洲，很多国家的市政厅、市长 办公室市民都是随便进去的，没有站岗放哨、戒备森严的。我去过的一些市政厅和市长办公室都非常的简朴，从规模、豪华程度而言比不上我们有些地方的乡镇办公 条件。如果市政厅很简朴，老百姓就很容易与你来往，如果你搞得过于富丽堂皇，老百姓就感觉你是个衙门口，距离也就拉开了。再看看宾馆，我去过以色列，他们 的人均GDP一两万美元，比中国高数倍，但最好的酒店说是四星级，实际上远远不如我们的三星级。看看中国吧，在有些开放的城市，步行十分钟的范围内，就会 发现十几家五星级、超五星级豪华酒店，政府的很多会议都安排在那里召开，政府的很多官员经常在那里开房间，这样的状态怎么能够联系群众。在我小的时候，我 清晰地记得，县委书记深入到农村，与农民同吃同住同劳动，吃饭采取派饭制度，就是今天在张家吃，明天到李家吃，那个时候农民很穷，没什么可吃的，县委书记 就跟着农民那么随便吃。住宿也是这样，与农民一起住，焦裕禄同志就是那个时期的典范。现在行吗？我实在是找不到一个类似的案例，去年终于发现了一个典型案 例，但不是在大陆，是在台湾，这个人曾经当过市长，是党的主席，他下乡的时候，与农民同吃同住同劳动，干完一天活，晚上就睡在猪圈里和牛棚里，这个人的名 字叫马英九！他现在成为台湾的领导人。<br />
&nbsp; &nbsp;&nbsp;&nbsp;　　<br />
&nbsp; &nbsp;&nbsp;&nbsp;　　<br />
&nbsp; &nbsp;&nbsp; &nbsp; 三是公务车越来越豪华。我在英国的时候，英国的中将以下都不配车，官员都不配车，军事院校没有一辆公务车，都是私家车，开到伦敦郊区停下来换乘地铁进城上 班。单位集体活动都是租车，租用出租公司的车，专业司机，准时准点，没有事故隐患。这样的管理，省去了大量的司机编制，抑制了腐败，节约了资源，提高了效 率，大大减缓了城市交通压力。北京市为了控制交通，采取了很多的法规，包括限行制度。但是，这些都是制标不治本的，真正管用的措施：一是在现有公务车的基 础上，同比例缩减70%以上（当然包括驻市内的军车）；二是确定一个汽车尾气的排放标准，凡是高于这个标准的车辆，一律不得进入五环以内，严重者强行限制 淘汰；三是数倍提高三环以内停车收费标准，过去一辆车停一小时花费5元，以后应该增加到15-20元，随着时间的增加收费要呈指数增多。<br />
&nbsp; &nbsp;&nbsp;&nbsp;　　<br />
&nbsp; &nbsp;&nbsp;&nbsp;　　<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;英国的将军带领学员到外出参观见学，身着迷彩服，乘坐出租公司的车辆，到达战场后徒步行进。印度是一个发展中国家，2001年我去访问的时候，发现包括上 将、中将所有将军的车，都没有空调，车辆的级别相当于我们夏利车的水平，他们的办公室没有空调，只有电扇。相比之下，我们真是汗颜啊！GDP不算高，国力 不算强，享受方面却走在世界前列，而这种享受不是普通民众，却是民众的衣食父母。不是张口闭口就说领导就是服务吗？既然是服务就要把自己当作公务员，而不 是衙门里高高在上的官僚！我是将军，给我配有专车和专门司机，如果从我开始搞改革，我愿意带头放弃专车和专用司机，我不会有任何意见。我不是在说空话，我 是在说我们如何才能做到立党为公，行政为民。当了官儿就一步登天，生活和物质待遇就一下子增长了那么多，那么多实惠还谈什么为人民服务啊！人民还愿意跟你 说真话、讲实话吗？<br />
