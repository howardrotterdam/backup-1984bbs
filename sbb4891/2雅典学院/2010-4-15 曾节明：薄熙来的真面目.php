																											<div id="postmessage_563991" class="t_msgfont">曾节明：薄熙来的真面目<br />
（《自由圣火》首发 转载请注明出处并保持完整）<br />
<br />
<br />
作者 : 曾节明, <br />
l表rg：4/14/2010<br />
<br />
　从大连“足球市长”算起，薄熙来登上政治舞台已不下十五年，是人是鬼、是驴是马，其实早已显露七八分，只不过“内行看门道，外行看热闹”罢了。<br />
　<br />
&nbsp;&nbsp;遗憾的是，在薄熙来显露七八分本色的当下，有些人和团体还在闭着眼睛硬说薄熙来是“江泽民的人”...这就连外行都不如了，一如他们至今瞎扯胡锦涛是“改革派”、“温和派”、“健康力量”...还在幻想胡“退出中共，另立新党”一样。<br />
　薄熙来是谁？这或许可以通过“另层空间”来判断――我不否认通灵相人术的先验准确性，但只有极少人有通灵相人的先验能力，而且，政治是现世现实的学问，因此，要判断一个政客，现世现实的分析仍然是依凭。<br />
　因此，要了解薄熙来是谁，决不不能靠“轱辘湖国王”之类的“另层空间”故事，否则，又是自欺欺人、误人误己；薄熙来既然和我们一样活于俗世，要认识他，惟有看他在这个俗世的成长经历、言行举止、行为方式。<br />
　<br />
　首先，薄熙来的升迁路线，处处闪耀着遮不住的才干：<br />
　主政大连期间，他招商引资成绩卓著，大连的经济增长速度明显快於东北其他城市，大连成为中国东北地区的第一大经济城市；薄熙来还多种手段并用，多方安置失业工人，点子很多，由此，大连较平稳的度过了东北国企改革中“大下岗”的艰危时期。<br />
　他还是中共地方官吏中施行“国际化城市规划”的先行者。主政大连期间，他仿效发达国家城市，制定并实施了系统的规划，全方位地改善了大连的自然环境、市容市貌和基础建设；薄熙来统治大连七年，将大连由一个计划经济时期遗留的工业城市，转变为一个国际旅游城市和国际会议及展览中心；在薄熙来的一手推动下，大连创造了“足球节”、“国际服装节”、“美容美发节”等特色文化品牌；大连也由此被誉为 “北方明珠”，被评为中国东北地区的最宜居城市和中国大陆的最宜居城市之一。<br />
　薄熙来改造大连的成功，令大连成为全国城市楷模，在九十年代末在全国掀起了一股“学大连”的城市建设风潮。<br />
　薄熙来突出的能力，加上其“太子党”的出身，完全压倒了当时大连市委书记，令大连成为全国惟一一个市长占据“一把手”的城市。<br />
　辽宁是计划经济时期重工业包袱最重的省份，2001年至2003年，薄熙来在担任辽宁省省长。上任之初，正值东北地区国有企业整合和转型时期，辽宁省大量国有企业处于停产状态，辽宁省失业率和“待业职工”数量全国最高，经济困难空前。薄熙来采取灵活的手段安置失业工人，又雷厉风行的推行“反贪打黑”运动，揪出沈阳市“慕马大案”，以生猛如虹的气势，将一批省市级腐败官员打落马下…这些措施，有效地收拾了人心、转移了视线、缓解了社会矛盾，使辽宁在如此困难情况下，于其任内没出大的事件，实现“维稳目标”。<br />
　尽管政绩卓著，薄熙来仍然在2004年被迫离开地方省份中头号要省辽宁，调任商务部长虚职，原有的要职让给胡锦涛的接班人李克强（因为此事，薄熙来心底对胡锦涛必然结下梁子）。上任商务部长之际，正值中外贸易摩擦抬头之时，其中中国与欧盟的纺织品争端尤为激烈，薄熙来使出浑身解数，通过与欧盟展开多轮谈判的方式，稳住了对方，缓解了危机。<br />
　虽然没有在大连时的那种风光，但逆境更能检验一个人的能力。薄熙来执掌辽宁和商务部，就象在比分落后的情况下打逆风球，他居然还能打得不错，不同的困境中，他总有办法化解危机，这显示出足智多谋和老练的行政管理能力。<br />
　就象任何野心勃勃的政治家一样，薄熙来不会错过任何出头的机会。2007年“十七”大后调任重庆市委书记，重获实权，他立即迸放出比在大连时更大的能量。他上任重庆的第一年，重庆市吸引外资就增长了百分之一百七十，达到二十七亿美元，从西部12省市的第6名跃升至第2名，2009年更飙升为西部第1名。2008年、2009年两年，重庆市的年经济成长率均名列西部第一。他在重庆推行的打黑除恶专项行动，既打黑、更治官，如今基本荡平重庆市民间黑社会势力，并给予黑恶官僚以重大打击…不管薄熙来“打黑”的真实意图为何，其“打黑”新政客观上的巨大的利民作用是不容否认的，其雷厉风行的气势、真抓实干的力度，为几十年来所未见，也因是，“打黑”运动受到全国民众压倒性的支持：著名的天涯论坛上，支持“重庆打黑”帖子、评论高达几十万条；有民营企业家为了表达感激，欲筹资1000万，发起成立全国首家“打黑基金”……<br />
薄熙来受到老百姓空前广泛的追捧，一跃而成为大国民意明星，恍若民主国家的当红总统候选人…在中国官吏普遍受到鄙视唾骂的今天，万马齐喑当中，惟薄熙来成就了一道独特的风景。<br />
　不光有着杰出的眼光和领袖能力，薄熙来还是一位天生的公众明星，他相貌堂堂、身材高大、长于演讲、善于煽情，其人极具幽默感，好做脱稿即兴演讲，其言辞妙趣横生、激情四射，极具煽动力…一如美国政治明星、国会领袖。<br />
2009夏，重庆八千出租车司机大罢工，这是中共建国以来规模最大的司机罢工，对薄熙来的执政能力构成了严峻的挑战。面对汹涌的群情，薄熙来出人意料地没有出动特警武警，他亲赴现场对话、邀请出租车司机代表座谈，同时邀请重庆内外媒体现场直播；面对摄像机镜头薄某人侃侃而谈、风度翩翩，他时而倾听司机诉苦、时而与代表一同计算运营成本、时而制止对司机畅所欲言的压制……<br />
　更为惊世骇俗的是，薄熙来还在座谈话上公开鼓励司机代表：成立重庆市出租车司机协会，以更好地维护自身权益。在现今胡锦涛中央对“非法组织”忌惮成了一根筋，连“钓鱼协会”都感冒的时候，薄熙来公然鼓动成立“非法组织”，可见其人的思维不拘一格、其胆识非比寻常。<br />
　不能不承认，这种民主、对话、公开、透明的作风，在中共官场中实在久违，一如紫阳重生、耀邦再世，恰如一股清新的风划过腐臭的死水塘，令人有恍若隔世之感。薄熙来一反胡锦涛暴力构建“和谐社会”的套路，以对话的方式平息了这次危机。薄熙来解决重庆大罢工事件绽现出来的能力和自信，令惯以钢盔、冲锋枪、封笔、断网解决问题的胡锦涛相形见拙、不堪入目。<br />
　薄熙来的政治风格，大有别于中共官场的俗套，与胡锦涛的僵尸蜡像风格形成了强烈的对比。可以说，如果在中国今天举行直选党魁、或大选总统，当选者一定是薄熙来，胡锦涛连提名都得不到。<br />
　但是，如果因为薄熙来的出众能力和开明表现，就以为他是开明派，那就错了。翻看薄熙来的仕途可以发现：这是一个非常善于投机的政客，多变、善变是其主要特征。<br />
　胡耀邦、赵紫阳年代，他以改革开放基层闯将形象一马当先，1984－1988年，他历任辽宁省金县县委副书记、书记，大连市金州区委书记，同时，还于1985－1988年兼任大连经济技术开发区党委副书记、书记，政绩卓著，八十年代的“薄书记”，完全一幅改革开放弄潮儿青年干部的形象；<br />
　“六四”屠杀后沉寂的两年，时任大连市宣传部长、副市长的薄熙来又变得一副极左的嘴脸，把“反和平演变”喊得震天价响；<br />
　邓小平“不问姓资姓社”后，官至大连市代市长、市长的薄熙来，很快又成了地方官吏中招商引资的能手、“解放思想”的先驱；<br />
　江泽民发起揭批法轮功运动后，刚刚还在高唱“与国际接轨”薄熙来，立即紧跟江泽民，成为地方官吏中、追随江泽民镇压法轮功的先锋干将之一；<br />
　胡锦涛流露出崇毛倒退、政治学朝鲜、古巴的狞狰嘴脸后，调任重庆市委书记的薄熙来，立即在重庆树立全国最高的毛泽东像，并发起“唱红打黑”、重温毛语录、毛批示及红色经典的运动…在当今极权倒退的逆流中，薄熙来大跳忠字舞、大掀崇毛热，高举毛泽东举得比胡锦涛还高…<br />
　从上可以看出：<br />
　一，薄熙来野心很大，为了权力，逢迎拍马不遗余力，敢于、而且善于投合中央一把手心意。<br />
　二，薄熙来的（如在出租车罢工事件中）的开明表现是为了树自己，树自己的目的是为了获取更大的权力。<br />
　二，薄熙来从不恪守任何政治原则，他见风使舵、灵活善变，表现出典型的机会主义投机政客的行为方式；薄熙来即使有政治信仰，也是次要的（至少在获得最高权力之前）。<br />
　虽然见风使舵，薄熙来却远非一般明哲保身的滑头官吏可比，他有雄心、有计划，且非常富于胆魄、心机和手腕。<br />
　这集中体现于重庆“治官”上。当今中国社会的几乎一切问题出在专制体制，治世须治官…这个道理，当然不止薄熙来一个人明白，但中国官场，只有薄熙来一人敢于大刀阔斧地“治官”。<br />
　他主政重庆的第一把火，就烧向了“领导干部”2008年3月，在他主持下，重庆市渝中、九龙坡等7个区县一把手进行了调整，而一年前，一场大规模的调整才刚刚完成。与此同时，“双规”包括重庆市规划局原局长蒋勇、原副局长梁晓琦、九龙坡区原区长黄云等一批贪腐高官。薄熙来新官上任，就以这种“官场地震”的方式鸣锣开道，需要无惧地方势力的气魄。<br />
　“重庆打黑”更是是一场大规模的、经典的“治官”运动，在薄熙来的强势主导下，重庆市权倾一时的黑社会保护伞――司法局局长文强、公安局副局长彭长健被毫不留情地掀翻，重庆市交管局局长陈洪刚、副区长赵文锐、检察院第一分院副检察长毛建平、经侦总队总队长陈光明、煤矿局副局长王西平等一大批黑恶高官短时间内被统统揪出。<br />
　公安系统，是和平时期政权的顶梁柱，也往往是政府中最为贪黑的系统，警界的地头蛇势力，强势的新官也得戒惧三分，薄熙来却敢于放手整治警察系统。紧接“打黑”而来的，是国内前所未有的一项铁腕治警“新政”，今年三月，薄熙来启动重庆市公安系统机构改革，一举将重庆三百多名处级、三千多名科级警官解职下岗，其所留下的三千三百多个位置空缺，由重庆全市两万三千名警察自由竞聘。这一翻江倒海般的举动，令重庆一大批警界贪官地震式垮台，而警察队伍中一批年富力强的较优者，则得到了喜从天降的上升空间。薄熙来砸向公安的铁锤，令腐败分子哀叹：薄熙来这是对公安队伍搞“大清洗”。<br />
　俗话说“强龙难斗地头蛇”，薄熙来却偏要斗倒地头蛇。薄熙来不怕得罪官，对越是气焰乖张的本土势力，他越是铁帚横扫，得罪到底。在薄熙来“治官”的铁腕和“狠劲”，令重庆地方势力哀叹：“官不聊生”。<br />
　中国地方官僚势力，根深蒂固、盘根错节、且与黑社会勾结；作为外地空降的一把手，薄熙来上任之初就大力整肃官僚队伍，这是要冒很大风险的，因为“治官”会引起地方官僚的仇恨，甚至激发其铤而走险，制造不测事件…因是之故，“治官”为官道所忌，凡新官上任者，几乎莫不以稳定队伍为首要。但薄熙来却敢于为了更高的目标，反其道而行之，不按常理出牌，薄熙来在重庆大规模地“治官”，充分显示出其冒险家的心理素质，薄熙来具备大政治家的胆魄。<br />
　但另一方面，薄熙来的冒险，却决不莽撞。“治官”、“打黑”之前，他把铁杆亲信王立军调至重庆公安局长位置，又从大连调来一批贴身近卫，不离左右；王立军既是得力副手，本人又武功高强，集“治官”杀威棒和镖王于一身。薄熙来此举，能有效消除危及自身的安全隐患，同时威慑体制内的仇敌和对手，令地头蛇无隙可乘，自己则可以放手“得罪人”，且“得罪到底”。<br />
　薄熙来赴渝后走的几步棋，看似鲁莽，其实富于心机，且步步为营：如果不重用亲信（王立军等人），则不敢治官；如果不敢“治官”，“打黑”就注定成为走过场；如果“打黑”成为走过场，薄熙来自己必然权威扫地。<br />
　权威出自别人爱戴，更出自别人的畏惧，镇压老百姓脸厚心狠的胡锦涛之所以至今没有权威，就因为他不敢动真格地整党。对此，薄熙来显然看得十分清楚，为了立威，他必须动真格地整官――薄熙来看得很准，在当下中共官僚集团遭万众唾骂、形象龌龊不堪之际，“治官”不仅能立威，更能极大地获取老百姓的爱戴。<br />
　对比薄熙来，一个显著的失败例子是汪洋。汪洋上任广东之初，掀起“解放思想”春潮，一时间，广东言禁放松、冤狱缓和、政改思潮萌动…可转眼间，投机毕现，“解放思想”竟成了学习和实践胡锦涛“科学发展观”的马屁运动，既不得民心、又不得官心，“新政”沦为假大空。<br />
　眼见薄熙来“打黑”的成功，接班人位置的另一有力争夺者汪洋，近期急忙在广东树起“打黑”旗帜，但因为缺乏薄熙来“治官”的胆魄，在广东官场更为顽固黑恶的地方势力抵制下，完全沦为只打民、不碰官的“打黑”――在深圳，没有靠山的民间黑社会是挖出了一些，但是黑社会在官场的保护伞却毫发无损，照样颐指气使…因此，这种打黑，不仅立不了威，反而招致老百姓更大的仇恨，让黑恶官僚的愈加轻。<br />
　缺了薄熙来的胆魄、心机和手腕，汪洋的“打黑”成了东施效颦。<br />
　凭借出众的胆魄和智谋，即使是“左倾”，薄熙来的表演也高出政治辅导员胡锦涛一大截。胡锦涛的左倾，是打官腔、说鬼话、学朝鲜，是封网禁言，暴力截防、是强拆征地、是军管断网…总之，是高举意识形态破旗，比毛共更为阴毒慎密、凶恶无耻地镇压老百姓。胡锦涛推出的政治洗脑材料《居安思危》、《六个为什么？》…其假大空和强词夺理，只能使更多的人反感现政权。<br />
　薄熙来的“左倾”，则密切联系现实，一定程度地关切老百姓疾苦：比如，他以重温“毛主席信访工作批示”的方式，减少暴力截防等暴政，缓和了社会矛盾，这种做法，不仅大得民心，而且能有效地把民众引诱到支持共产党统治的立场上去。<br />
　他觑准当前的信仰危机，将毛泽东语录用新瓶装旧酒的方式，向民众推销，大获成功。在薄熙来的主导下，重庆市以唱红歌、读经典、讲故事、传箴言等多种方式兜售毛泽东思想，政府规定：每个区县至少提供1个大型红歌坝坝舞广场；全市所有图书馆至少要提供一间经典阅览室；每个镇街至少设立1个“红色故事角”；各区县都要设置“箴言栏”……<br />
　薄熙来亲自编撰“经典读物”，定名为口语化的《读点经典》；在这本小册子中，薄熙来把毛泽东诗词文章和《论语》、《孟子》篇章、唐诗、宋词、西方思想家名篇等奇特地掺杂在一起，并亲自作序，吹嘘此书“有如多种维生素片，虽每日一粒，亦可养身。”据说，这本大杂烩小册子已出至第六期，薄下令重庆近三万干部人手一本，该书并向市场发售，据说销量已高达三十多万册。<br />
　还亲自上阵，别出心裁地于2008年创设“重庆市红色短信大赛”，在2009年第二届比赛上，他还亲自发出两条“毛主席语录”，作为自己“参赛短信”，其曰：“我很喜欢毛主席的几句话:‘世界是我们的，做事要大家来’，‘世界上怕就怕认真二字，共产党就最讲认真’，‘人是需要有点精神的’，这些话很精干，很实在，也很提气。”<br />
薄熙来的这条“红色短信”转发已超过1600万次，足敌政治笑话和黄段子。<br />
　薄熙来非常狡猾：由于毛泽东思想中含有反特权反官僚的民粹成分，非常符合现今弱势群体口味，且毛的篇章言论富于文采和哲理性，因此薄熙来舞弄毛语录，能够恰好填补社会的信仰真空；而中共官僚集团普遍人文素养低下，为掩饰不学无术，急欲文化美容、附庸风雅，薄熙来所炮制的大杂烩小册子可读性强，也适合他们需要。另外，采用手机短信这样现代通信手段“扬红”，又较能吸引年轻人参与。<br />
　以上薄熙来左倾作为，无疑是新时代的毛共洗脑。即便是这种僵尸般陈腐的红色洗脑运动，薄熙来却能够搞得有滋有味、个性化十足，大有别于胡锦涛面目可憎的“保先运动”。<br />
　长远来看，中共政权要想维持，就必须用红色文化塑造新愚民群体；而塑造新愚民群体的功力，薄熙来远在胡锦涛之上。<br />
　明眼人不难注意到：薄熙来重庆“治官”、“打黑”、“唱红”的另类结果是“薄书记辛苦了”，他投合胡锦涛的左癖，却大树特树他自己，风头盖过“胡核”，却让“胡核”抓不到把柄，<br />
　综上可以看出：薄熙来之胆魄和智谋，在现今中共官场中无人能及。这就产生了取而代之的巨大天然诱因，薄熙来能否抗拒权力的引诱？这就要看他是诸葛亮，还是司马懿了。<br />
　<br />
　薄熙来是不是诸葛亮？这在文革中可窥一斑。薄熙来是中共元老薄一波的的次子，薄一波虽然算老革命高干，但在毛共时期仅为计委主任，薄家特权不是很大，薄熙来家庭，当时在高干子弟中根本不起眼。文革中，薄一波被打成“七十二个叛徒”之一，薄熙来父母遭残酷批斗，其母不堪凌辱，自杀身亡，<br />
　薄熙来文革时积极参与对其父的批斗（据说踢断了薄熙来的三根肋骨），（不管是不是真的）亲自批斗其父，这足以说明其人心狠肠硬，毫无孝心。薄熙来之所以痛殴其父，有两种可能的原因：一是毛共洗脑毒入骨髓，以致六亲不认；二是显示与其父划清界限的立场“坚定性”，以求个人前途。<br />
　薄熙来显然不是容易被洗坏脑子的政治傻瓜：从大连到重庆，他从没有实质性的左倾行为，他近年的“崇毛”，只不过是作秀。<br />
　可见，薄熙来之所以痛殴其父，更多地是第二种原因，薄熙来对父亲的凶狠，集中反映出：薄熙来是一个为了个人前途不择手段的人。<br />
　中国古话说：“不忠不孝”。这个词造得很贴切：不孝的人往往不忠，在现实当中确实如此。不孝的人为什么往往不忠？因为除了特殊时期被共产党洗坏了脑子的傻瓜以外，很难想象一个对自己双亲都无爱心的人，会爱戴自己的领导。“不忠不孝”的人物，历史上最典型的例子莫如吴三桂。为了争夺一个小妾把双亲送上绝路、继而背叛国家、民族，引野蛮外族入寇、把中国推落亡国的血海火坑...终其一生：事明而叛明、降闯而叛闯、降清而叛清，诚可谓不孝之至、不忠已极。<br />
　薄熙来有没有机会成为吴三桂不清楚，但他对落难父亲的本色，清楚地显示出他不忠不孝、脸厚心黑的本色――他薄熙来不会忠于任何人、任何组织：他不是毛泽东的人、不是邓小平的人、不是江泽民的人...更不是胡锦涛的人，最终，他也不会是共产党的人。<br />
　薄熙来之“治官”、“打黑”、“唱红”…是为了构筑自己的权势，为今后问鼎中南海做准备。为了入主中南海，他必须树立压倒胡锦涛、习近平等人的个人权威，<br />
　从他对父亲的狠劲可以看出，要是胡锦涛挡了他的道，胡锦涛该断的就不是肋骨，而是人头了；要是共产党（游戏规则）挡了他的道，搞垮共产党他绝对在所不惜。而薄熙来的能力、胆魄、心机、手腕…恰恰又可能做到这一点。<br />
　薄熙来决不是是诸葛亮，他更像曹孟德、司马懿、袁世凯。<br />
　“治世之能臣”，“乱世之奸雄”，薄熙来貌合之、神似之：雄才大略、通权达变、脸厚心黑、善用规则、善应时势，薄熙来象极了当年袁世凯。<br />
　当今中国，新袁世凯的产生是不可避免的。如果中国有大选制度、中共有党内民主制度，薄熙来或许能够成为中共政权的救主；但是，中共现行的这一套连党内民主都丝毫不容许的极权体制、胡锦涛本人的僵硬颟顸，却注定堵死薄熙来们的任何上升空间，把野心勃勃的薄熙来们逼成曹孟德、司马懿、袁世凯。<br />
　<br />
　因此，共产党崩溃不可避免的，薄熙来的崛起是无可阻挡的；中国今天这样一个毫无共识、民主素养连民国初年都远不如的乱民社会，正是滋养乱世奸雄的最肥沃土壤。<br />
　希望薄熙来先生能汲取袁世凯称帝的历史教训。<br />
<br />
