																											<div id="postmessage_789257" class="t_msgfont">大饥荒时安徽亳县人吃人见闻录 <br />
<br />
<br />
&nbsp; &nbsp;人吃人的惨状<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;梁志远在《关于“特种案件”的汇报DD安徽亳县人吃人见闻录》中记录了大量的人吃人的惨状。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;该文称，在农民大批非正常死亡中，人吃人并不是个别现象。“其面积之广，数量之多，时间之长，实属世人罕见。从我三年近百万字农村工作笔记中查证和 我自己耳闻目睹的事实来看，绝对没有一个公社没有发现吃人的事，有的大队几乎没有空白村庄。”这个严重问题是由少到多，到1960年4月达到顶峰。有时路 上死人被人埋后，一夜就不见尸体了。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;有些地方，农民家里死了人，为了防止被人扒吃，就守坟多夜，待尸体腐烂发臭为止。有的吃人家的死人，有的吃自家的死人；人肉有吃熟的，也有吃生的； 有吃死尸的，也有杀吃活人的；有吃自己搞来的，也有从市场上买来的（多为熟肉）。在城郊、集镇、村头摆摊卖的熟猪肉中，有不少是人肉冒充的。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;在吃人肉的人当中，约有40％死亡(引起中毒后腹泻)；另一些人吃人肉而没出事主要是因为吃瘦弃肥、肉菜混吃、少食多餐、腌咸后吃等。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;针对人吃人的情况，县政法部门也惩办了一批，先称“破尸案”，后按上级指示，统称“特种案件”。这种处理是先严后宽，以后就不告不理，不了了之。这 种案件能办不能说，对外只字不漏，对上汇报慎之又慎。稍有泄露，就大祸临头。在文化大革命中，提及此事的人也受到打击。因此，这些情况一直不为外人所知。 梁志远在这篇文章中提供了一些案例。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;县委亲自处理的第一起“破尸案”<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;1959年春，城关公社涡北派出所抓获了一起正在煮死小孩肉的盲流农民，遂将“犯人”和小孩肉送到县公安局。公安局当时不知如何处理。一位副局长向 县委第一书记赵建华作了汇报（当时梁志远在场听了汇报），当即定为“破尸案”，并决定逮捕“犯人”。县委政法书记李庭芳亲自审讯后认为，“犯人”身体瘦 弱，无政治目的。于是未经请示县委，发了两个馍，将“犯人”教育释放。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;县委知道后，李庭芳受到严厉批评。李又让公安局将“犯人”抓回，重新入狱。经过半个月的审讯，确定“犯人”没有政治目的，县委批准将其释放。本案到 此终结，但办理此案的派出所所长李玉贤，因向别人说过历史剧《打銮殿》中的剧词：“人吃人，狗吃狗，老鼠饿得啃砖头”，在1959年的反右倾斗争中，当作 右倾机会主义分子进行批斗，并开除党籍，撤销职务，降两级，调出公安系统。李庭芳也被调去带民工到皖南修铁路，再没有回亳县工作。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;饥饿难忍吃亲人<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;叔父吃侄女。据民政局离休干部葛现德回忆，其家乡城父公社葛鱼池生产队有一名社员，吃了其侄女的肉。1960年，其兄嫂死后，一个不满10岁的侄女被其收养，不久侄女饿死，被其吃掉。因吃人肉，精神紧张，患病多年。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;儿子吃父亲。1960年5月25日，县委生活检查组长孙振林（中共党员，县文化馆副馆长）等人汇报：在魏岗公社逯楼大队陈营村检查，群众强烈反映农 民马某，在其父亲死后被煮吃掉，并将一部分充当猪肉以每斤16元卖掉。由于当时政府对这类事采取回避态度，所以没加追究。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;父母吃儿子。1960年3月28日，双沟公社王阁大队王庄王某，在小儿饿死后，被煮吃，吃后夫妻两人腹泻，三天后二人均死去。全家七口饿死五口，剩下两个孩子被送进大队孤儿园。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;母亲生吃女儿肉。1960年春，大杨公社刘匠大队朱寨村朱李氏，在全家4口人已饿死3口的情况下，自己饿得无法忍受，就在死去的女儿身上啃掉几块肉 吃下去，因此引起腹泻，拉出许多烂肉。当检查人员任怀赞（大杨公社烟酒专卖主任）发现时，该人已全身浮肿，处于半昏迷状态，但知道要馍，要饭，要医，要 药。生产队长朱本善把真实情况告诉检查人员，并说昨天刚把生吃的女儿埋在地里。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;据张催粮回忆，1960年春，我家观堂公社集东一里张庄张韩氏，全家4口人，饿死两口之后，身边只有一个瘦弱的女儿，她迫于饥饿，丧失理智，打死了女儿，将其煮吃，之后精神失常，有时呼叫女儿的名字。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;据市农业银行离休干部、中共党员王体忠回忆：他家在五马公社泥店西南王楼村，社员王某的老婆（1922年生人）在1960春因饥饿打起了十来岁女儿的主意，将女儿打死煮吃。此人在90年代初还健在。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;父亲杀吃亲生儿子。据大杨财政所离休干部、中共党员孙传玺回忆，大杨公社丁国寺西南邵庄孙某（孙传玺妻子娘家的邻居），1960年将其亲生儿子小秃子打死煮吃，全家8口人先后死光。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;据法院原秘书尚振华（离休干部）回忆，他所经办的案件中，有一个先吃小孩的惨案。案犯是大杨公社钓台村农妇张某，1960年吃掉自己的死孩后，不久又打死邻居的小孩吃掉。案发后被捕，判决后死于狱中。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;梁志远的一个亲戚吃了死去的儿子，夫妇腹泻病危，其婶母说他们不该吃人肉。他说：“情愿一顿吃饱死，不愿长饿活着生。”结果两夫妇死亡，全家7口人死去5口。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;哥哥杀吃弟弟。据市技术监督局、中共党员、退休干部杨心宽回忆：1960年春，我在城父公社任组织部长，该公社龙台庙大队韩老家村，韩某（16 岁），其父母死后，即与其弟韩四生活在一起。1960年3月，他因饥饿将其弟弟打死，头和脊背放在泥圈里，肉放在锅里煮时被干部发现了。经审问供认杀弟煮 吃的事实。因未满18岁，公社党委作决定时我参加研究，将韩某抓送公社“火箭营”（一种强化劳动的组织）扣留关押，数月后在“火箭营”死亡。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;据亳州市原书记李兴民回忆：我家原住亳县大杨公社郭万大队李老家，与前瞿庄石某家相距很近。石某家是一个18口人的家庭。1960年春，18口人饿死近一半的时候，老三的小孩死了被留下煮吃。全家人都吃了，全都腹泻。除石有亮一个活着以外，其他人全都死去。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;一些多户吃人的村庄：<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;据古城公社沼北大队原副大队长、共产党员马占坤回忆：1960年春，该大队河西王村多户农民吃人肉。大队多次检查屡禁不止。王某的母亲屡教不改，被大队书记耿某打后送公社“火箭营”关押致死。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;据县委生活检查组长、中共党员陆美（女，已离休）在1960年3月3日汇报：魏庄公社蒿庄村，全村40多户有25户吃人肉，严重时几乎每天夜里都有人下地扒死尸。社队都知道此事，认为无法制止就听之任之。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;观堂公社集东一里张庄是一个多户吃人肉的村庄。他们吃人肉是半公开的。张某氏，用人肉加稀饭救活了近门的一个孤儿张催粮（即回忆本村张韩氏杀吃女儿的张催粮），张催粮现已年近半百，有时还谈这事。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;埋不住的盲流尸体<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;1960年4月，梁志远去县劝阻站（县城到西北角、南京西路北）检查盲流人员的生活情况，站内医生杨文德说：“劝阻站盲流死亡人员被扒吃了不少。” 随后，梁志远与杨医生一起去坟地看，确实有不少被扒的站盲流人员墓坑，地上烂衣狼藉，还看到一未理发的中年男子的人头。梁问杨医生：“公安人员怎么处 理？”杨说：“我已向公安局反映过多次了，没人表态。”<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;一次批斗吃人肉者的大会<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;据立德教育办公室会计、中共党员李士远回忆：立德公社杨王大队李寨村，1960年春，吃人肉的越来越多。大队、生产队制止不了。在无可奈何的情况 下，采取了&quot;抓典型&quot;大会批斗的办法。抓了一个多次吃人肉的老中农李某的老婆，召开群众大会进行批斗，要求人人参加，不参加者食堂扣饭。几个干部在会上发 言。指责她“犯法”，“往政府脸上抹黑”等。还说：谁再这样（指吃人肉），就把谁关死在监狱里。批斗会后，吃人肉的就少了。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;一场抢吃人肉的闹剧<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;据农业银行离休干部、中共党员王体忠回忆：1960年春，他家乡五马公社泥店西南王楼村，有不少农民因饥饿而吃人肉。为制止这种情况蔓延，大队下决 心抓典型示众。该村王某的前妻某氏，多次吃人肉。有一次刚把煮熟的人肉捞到盆里，被干部查获，把人和人肉一起送到大会场里，立即召开群众大会对她进行批 斗。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;参加大会的人闻到香喷喷的人肉，想吃又不敢吃。有个大胆的人说一声“我尝尝”，伸手拿了一块人肉大口吞食，接着众人一哄而上，你抢我夺，乱成一团，转眼间一盆人肉抢得精光。王体忠的妻子也抢了一块，当时吃了感觉很香。批斗大会无法开下去，只好宣布散会，不了了之。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;人肉的市场交易<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;1960年春，由于吃人肉的情况不断发生，人肉的市场交易也随之出现。城郊有，集镇有，农民摆摊设点和流动串乡卖人肉的也有。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;1960年3月下旬，城父公社党委电话汇报：丁楼大队李楼生产队农民吕某，于3月17日至21日，共扒掘三具女尸，煮熟后充当猪肉，在菜桥闸工地摆摊出售。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;1960年春，核桃林场王庙林区邢庄邢某，常以人肉充当猪肉串乡出卖，在当地广为人知。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;1960年春，十九里公社薛菜园大队任寨农民周某，以人肉煮熟充当猪肉在本村出卖，被县委农工部科长李延荣等人发现后令其埋掉，周某害怕追查，当即逃跑。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;1960年春，五马公社黄营大队郭桥村，一个57岁的农民杀死本村13岁男孩连臣，当夜煮熟自己吃了一部分，第二天在村头路口当猪肉出卖。县公安局田朝珍等人侦破此案，将犯人逮捕，判处死刑，执行前死于狱中。</div>

							
							
							
							
