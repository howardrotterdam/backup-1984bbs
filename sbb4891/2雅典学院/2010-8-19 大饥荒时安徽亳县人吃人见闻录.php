																											<div id="postmessage_791312" class="t_msgfont">人类自己的警示<br />
<br />
人是地球上最霸主的动物，不管什么东西只要想要，就没有要不成的，只要想吃，就没有吃不到的。长期以往惯出了霸道的毛病，人类为了自己的利益，在地球上进行非理智的开采、掠夺和战争，屠害生灵和植被。现今很多动物、植物、昆虫、矿藏、水源没有了，地球正在遭受到前所未有的打击，工业和城市化的飞快发展，污染成了威胁人类生命、生存最大敌人，空气正在一天天地变成毒气，奇怪的病例正迅速侵蚀着我们的身体。<br />
<br />
SARS阴影未远，欧美国家的疯牛病，亚洲国家的禽流感又接踵而来，病毒学家呼吁，如果人类再不尊重大自然，这些病毒和细菌的困扰将永不止息。人类打乱了自然生态，压根儿也没想到子孙后代，最终，人类要担负起对自然的责任，否则造成的苦果就要自己品尝。请看以下几个故事。<br />
<br />
1913年，德国汉堡一个修道院的女仆，23岁，突然精神病发作，尖声大叫，神情呆滞，浑身抽搐，大笑不止，吞咽困难，卧床不起，不到两个月就死于癫痫。一位叫克罗伊茨费尔特的德国医生解剖了她的尸体，发现她的脑部没有发炎，却严重受损，有不知名物质杀死了数以百万计的脑细胞。他意识到这是一种新的疾病，但没有找到病因。1920年，他的论文发表时，引起了另一位名叫雅各布的德国医生的共鸣，―年前，在他的手上也死过类似的病人。从此，这种新的危险的脑部疾病就被命名为“克雅氏病”。<br />
<br />
1950年，赤道几内亚东部的南富雷山的夜晚，月白风清。一群有着乌黑皮肤的妇女带着她们未成年的孩子，将一具老年妇女的尸体拖进一块马铃薯地里。她们都是女性亲戚，心中充满怜悯，也充满了期待。不一会儿，在死者的周围，篝火点起来了，在熊熊的火焰照耀下，一场盛宴开始了。<br />
<br />
几年之后，一位来自美国的儿科医生及病毒学家，后来获得了诺贝尔奖的加得塞克来到南富雷。不过，吸引他的不是吃人的盛宴，而是发生在这里的一种新病症――库鲁症，也叫“笑死病”。这种病的典型特征是：吃自己亲人的肉的妇女和儿童，第一个月步态蹒跚；第二个月颤抖，傻笑，说话不清，不能自理；第三个月全面瘫痪。接着是失去吞咽能力，活活饿死，渴死。最痛苦的是，在这一切发生的过程中，病人始终相当清醒。<br />
<br />
最初，加得塞克研究的对象是库鲁症，它和克雅氏病有相近之处。1959年，加得塞克收到一封来自伦敦的信，写信人叫海德娄，一位伯克郡的兽医，专门研究一种绵羊的古老而神秘的疾病――羊瘙痒症。通常得这种病的是羊不是人，但是，它们得病后的症状：走路不稳、颤抖、眼瞎、摔倒、直到死亡，都和人类的库鲁症有相似之处。这种病第一次出现于1947年，美国密执安州的一个农场从加拿大引进了种羊，接着就发生了大规模的羊瘙痒症。美国农业部展开了大规模的屠杀，一群羊中只要有一只病羊，就全部杀死。但是，他们还是没有控制住病情的蔓延，甚至跨越了品种，蔓延到山羊当中。后来，在海德娄实验中，这种病症又跨越了物种的屏障，感染了貂类和灵长类动物。羊肉是人类的食物，这种羊瘙痒症会不会感染人类呢？可惜海德娄不敢用人做实验，只能留下一个令人担忧的遗憾。<br />
<br />
1985年，英国一位叫惠特克的医生接到一个农民的电话，说他家的一头母牛行动怪异。在那个叫做普仑顿的地方，惠特克看到病牛有很强控制的攻击性，身体协调性很差，站立不稳，东倒西歪，很快就毙命了。一种发生在牛身上的新的疾病出现了，专家们将它定义为“牛脑部海绵化病”，也就是“疯牛病”。<br />
<br />
1987年，疯牛病蔓延到英格兰和威尔士各地，惟独苏格兰幸免。越来越多的科学家加入到了研究疯牛病的队伍。他们看到，在苏格兰以外地方，许多动物尸体处理的工厂里，到处弥漫着蒸气、鲜血、油脂和臭气。人们把肥肉、骨头、内脏、头、尾巴、血、牛、羊、猪的尸骸，甚至家禽的羽毛，放到大锅里提炼黄油，剩下的油滓用庞大的机器磨碎，制成肉骨粉。再用这种饲料去喂养提供这些原料的动物，生产廉价的奶和肉。<br />
&nbsp; &nbsp; 科学家们开始呼吁停止让食草动物吃肉，政府开始下令大量屠杀牛。人们还展开了一场疯牛病是否会蔓延到人类的争论。当人们争论不休的时候，事实说话了：<br />
&nbsp; &nbsp;&nbsp;&nbsp;1993年，15岁的女孩维姬5月发病，8月死亡。她的脑部切片显示海绵质脑病变，医生告诉她的祖母，这就是疯牛病。<br />
&nbsp; &nbsp;&nbsp;&nbsp;1994年，英国一位16岁的女孩，说话含糊不清，平衡出了问题，记忆衰退，走路摔跤。死后，医生发现了她的大脑皮质呈现海绵质病变。平时她吃羊肉，也吃腌牛肉和汉堡包。<br />
&nbsp; &nbsp;&nbsp;&nbsp;1994年，一年轻人向医生诉苦，说他恐惧水和尖锐的物品，步伐常常失去控制。他死后，在解剖他的脑部时也发现了海绵质脑病变。此前，他有8年时间在他姑妈的农场，喝未经消毒的牛奶，跟母牛接近。但是，那群牛没有出现过疯牛病。<br />
<br />
1996年，加得塞克总结说：“人类完全不懂人类被什么感染了，其实就是库鲁症，所有物种都会被感染――乳牛、肉牛、猪、鸡。这种病没有在猪身上发现，只因为人们不会把猪养上七八年，顶多两三年就宰掉了。我们在实验室里让猪染上羊瘙痒症，养到第8年，它们就发病了。”“不仅猪肉有问题，那代表所有的猪皮皮夹、猪肠做的手术缝线，所有喂肉骨粉的鸡都已受到感染。素食者吃了用鸡粪当肥料的蔬菜，也会传染上。”<br />
&nbsp; &nbsp;&nbsp;&nbsp;1996年，另一位科学家雷熙有证据显示：家畜仍然会发作疯牛病，而处于潜伏期的带病家畜也还在供人食用。人类克雅氏病的潜伏期可达25年，甚至30年。所以人类发病的流行巅峰大约是在2015年。如果目前克雅氏病变种的案例平均每年增加50%，那么到时一年的病例就多达20万。也就是说，每年要死20万人<br />
<br />
素有“冠状病毒之父”之称的台湾学者赖明诏表示，大部分的病毒，都是由动物传给人类的，艾滋病是猴子传染的，埃波拉病毒也是如此。惟独天花病毒，只有人类身上才有。其它病毒从动物身上传给人类后，经过基因突变，人类便受到感染，且极难治愈，有很高的死亡率。<br />
<br />
他还指出，因为病毒在动物身上，人若不和动物接触自然没事，但因为人口太多，与动物接触频繁后，病毒自然会产生突变能力，适应不同环境，感染人类后继续繁殖，引起重大疾病，再经人与人接触彼此传染，最后引发在流行。<br />
<br />
在这场人和细菌、病毒的战争中，人类赢不了病毒或细菌。人类发明抗生素药物，又滥用抗生素，使病菌产生抗药性，从而使病菌愈来愈来强壮。人类开发环境，侵扰在自然，病毒的反噬永无止境。人类要学会与病毒和平共存，不要去侵犯自然，就能相安无事。<br />
<br />
以禽流感为例，病毒基因为RNA，有8段基因体，禽流感基因体随时可以交换，就会突变成人的流感病毒。当一个细胞被病毒感染之后，几个小时之后病毒就开始繁殖。禽流感的病毒称为H5N1，和人类的流感病毒H3N2、H3N1是基因体交换的产物。不同的基因体是从不同的流感病毒来的，如果这些基因体变成人的流感病毒基因体，就会生成人的流感病毒。艾滋病、出血热与猴子有关；猫抓病则来自猫科动物；莱姆病来自三十余种野生动物和家禽；十余年的疯牛病让英国举国不宁……对于食尽天下野味的国人，动物们似乎正以SARS病毒报复着我们，这难道是我们咎由自取？要克制这些不断衍生的怪病，研发新药只是治标和暂时的方法，治本之道还是尊重自然。保持动物和人类应有的距离和空间，才是降低威胁的最佳方法。就目前状况养生是提高自身免疫功能抵抗疾病的唯一办法。<br />
<br />
在生态文明时代，人类应选择一种与自然和谐共处的发展方式，即可持续发展方式，既使人类的发展需要得到满足，又使自然界的生态系统得到最大限度的保护。建设生态文明，是我们的任务。要实现这个目标，需要全社会的参与。这不是天方夜谭，而是被严谨的科学调查和研究证明了的事实。它在警告我们：如果我们不想以人类的生命为代价，就不要利令智昏地去改变那些不该改变的事情。</div>

							
							
							
							
