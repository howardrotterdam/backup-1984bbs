													<h2>观耘闲人：中共内部面临大分化</h2>
						
						
																											<div id="postmessage_520594" class="t_msgfont">----温家宝清唱“民主”独角戏<br />
<br />
作者：观耘闲人&nbsp; &nbsp; 文章来源：《动向》月刊<br />
<br />
中共高层面对社会高危状况，全然失去了政治改革愿望。在政改与稳定之间，各个派系没任何分歧地选择了后者。此中，左派对普世价值不遗余力的批判，也是以促使右派把党的生存利益放在社会利益之上为宗旨的。<br />
<br />
从战略学的角度看，中共由於在全球经济地位的提高，会逐步放弃制度上盯住美国的许诺，进而更加专注地开发所谓的北京共识。正如美国发现学会创始人乔治？吉尔德就中国互联网管制政策所作出的分析那样，“作为一个专制政权，它显然不可能接受一个开放和匿名的网络制度。”<br />
<br />
温家宝在玩“刘备摔孩子”<br />
<br />
然而，不管中共高层在政改与稳定的选择上多么地高度一致，在其中下层特别是素有传统士风精神的知识分子当中，改革的呼声还是相当高的，且持续不断。<br />
<br />
在二○一○年三月的全国人大与政协两会召开之前，体制内作家顾晓军在《博客中国》上公开发表文章，要求培养反对党与党内反对派以遏制越反越腐的腐败势头。鉴於民间对民主政治的诉求日高一日的实情，当局怕引起新一波的政治抗议，因此没有删除顾的网络文章。当然，有分析家认为这可能是北京最高决策层为两会营造“民主气氛”而安排的一场政治戏剧。<br />
<br />
不管北京最高决策层是否安排了政治戏剧，温家宝在二月二十七日作客新华网暨中国政府网的在线亲民行为确是在玩弄权术。<br />
<br />
关於三国的传统曲目中有《长阪坡》一剧，剧中有刘备为险失上将赵云要摔死赵云所救刘禅之情节。民间缘此编出俗语，称“刘备摔孩子──盗买人心”。温家宝古典文学修养颇深，对此剧此语t解透彻，兼之新华网与中国政府网一套人马两块牌子，都是温管辖内的媒体，敢不好好配合？<br />
<br />
温在作客两网时，蜻蜓点水式地讲“只有民主才不会人亡政息”，但是丝毫不敢涉及普世价值，可见顾忌颇深。对於百姓最关心的公平问题，温也是漫天许愿，或说做大社会财富这个蛋糕是政府责任，或说公平分切蛋糕是政府良知。如此之说，只能略博一笑而已。因为就连官方自己公训牡鞑榻峁都显示公务员的道德水平低於妓女，公信力陷於全面危机的政府怎么可能公平分配蛋糕呢？！<br />
<br />
退一步说，就算温的个人道德水平无可挑剔，他也不可能在剩余的短暂任期内解决人民最有意见的公款吃喝、公车私用、公费旅游之三大顽疾（俗称“三公”），就更不用说政治改革、推进民主了！所以，温在回答网友提问时，连行政改革都不提了。<br />
<br />
田纪云贬斥“科学发展观”<br />
<br />
自从纪念改革开放三十年的胡锦涛“不走邪路、不易帜”表态后，中共意识形态全面左转。胡力欲平衡左右之争，亦讲“坚持改革不动摇”，但是改革派受到的打击是前所未有的，这种倾向引起了老改革家们的高度注意。两会召开之前，原中共总书记赵紫阳的得力助手田纪云，在体制内最有影响的杂志《炎黄春秋》头篇位置上公开了一个谈话，田在谈话中毫不客气地说：“那些自以为是马克思主义的理论家们，也应该有点自知之明。现在除了邓小平的理论能够动员群众，号召群众，凝聚群众外，任何盗名欺世的所谓「理论」，可以说根本没有市场，人们只不过嗤之以鼻。”<br />
<br />
温家宝没发明什么理论体系，但是作为与田有不少共同政治语言的政治家，温也策略性地承认了田的观点。温与网友交流的话语中引述过毛在延安时期与黄炎培的政治对话典故，此前，田的谈话也引述了该典故。<br />
<br />
但是，无论田还是温，谁都没法推进实质性的政治改革，因为中共本身已经完全利益集团化，倡言以与利益集团切割为反腐重磅措施也是伪命题，其情状正如上述吉尔德的判断一样。胡温无可奈何的政治倒退不仅使得党内改革派严重受挫，社会不满情绪急剧膨胀，而且学者型官僚对此也忧心忡忡。如国务院发展研究中心对外经济部部长隆国强告诫说：“当前中国开放的基本格局是约十年前加入WTO谈判时大体确定的，与目前中国国际分工中的地位、综合国力、国际竞争力和转变经济发展方式等要求相比，对外开放已经明显滞后。”<br />
<br />
“群众监督”仍是无稽之谈<br />
<br />
温的民主、人权、政府良知诸话题虽多是轻描淡写，但总还是给寄希望中共用党内民主推进社会民主的那些人以鼓励，使他们不致彻底失望。基本常识告诉人们：这一切都离不开群众监督。温在解答“三公”问题时，也确实讲到“任何一项行政性支出都进入预算，而且公开让群众知道，接受群众监督”，但是，实行起来则困难重重，因为那些能够行使监督权的所谓群众无非都是共产党层层官僚机构挑选的人。这也是人民代表并不代表人民的奥秘所在。<br />
<br />
平常百姓，哪怕是一些没有政治身份的专家，是很难获取准确的政府信息的。参与社科院一项专门调查的法学研究所的副研究员吕艳滨对媒体透露说：“一些政府门户网站履行政府信息公开条例规定的情况不好，不少政府网站存在信息不集中、网站信息获取不方便，个别政府网站只重形式不重内容等问题，按照我们设计的测评指标，四十三个城市中半数以上不及格。”吕所涉及的研究报告在北京两会前已经做完，据悉也得到了温家宝在内的数位高层人士的肯定。<br />
<br />
不容乐观的现实是：中央政府不可能贯彻一个细节标准详备的评价体系，更何况中央政府仅仅在统计数字的准确性上就无法说服公众。地方政府与新闻媒体合谋，可以十分容易地肢解公众知情权。比如河北正定的古城门大火事件发生后，处理了五名官员，新闻媒体上没刊登任何一个人的全名，连姓氏也没出现，只是列举了被处分人的职务。<br />
<br />
国外一些驻京新闻媒体熟悉中文的记者不得不按中国网民指点的路径进入百度的“正定吧”，即使这样仍然没能得到五个人的名字。香港一家亲共媒体的驻京记者试图以访问正定信息港（政府网的代名）的方式，获取正定文物旅游局的信息，但是进入信息港后却无法进行对正定文物旅游局的二级访问，而访问其他同级机构则没有遇到障碍。该女记者不得不一改过去轻视大陆异议人士的做法，反过来求“神通广大”的异议人士t解详情。没想到，异议人士认为她是帮中共当局钓鱼执法而予以拒绝。<br />
<br />
中共不可避免要分化<br />
<br />
中共作为一个超级庞大的利益集团，内部产生剧烈权争不可避免，也就是说分支派系的利益维护越来越刚性。可靠消息表明：两会前，中共情治机关试图控制两个非法组织的头目，但是由於内部意见高度分歧，没能执行下去。那两个被情治机关认定的非法组织，一个叫“中国毛主义共产党”，一个叫“中国工人共产党”。<br />
<br />
前者坚持毛的反对资产阶级法权的意识形态，认定邓小平改革开放以来的中共是修正主义集团，主张彻底清算；后者则拥护邓小平路线，但坚决主张社会重申公平。据两者的高级理论人士在联络民间理论家的谈话时自称，他们分别得到了党内重量级人物的支持。更有消息说：前者的“精神领袖”是毛的孙子毛新宇，后者的“精神领袖”是现任重庆市委书记薄熙来。但是，毛新宇与薄熙来均未出面承认或否认这样的传说。<br />
<br />
温家宝在两会期间利用网媒优势，一个人出来说话，其背后的含义是，没人愿意和他一起出来公开亮明政治观点。<br />
<br />
海外一些亲共战略学家除了劝说北京高层接受“民主是普世价值”的观念外，还苦尽孤忠，让后者注意党内政治拉美化倾向，要给薄熙来那样能量级的党内高官应有的新地位，也要给以贺卫方为代表的自由化势力以应有的政治地位和话语权。这一切均表明中共内部实质上已经高度分化，分化的公开化只是迟早的事情，或许二○一○年就会发生重大事件。因为就目前的状况看，中共党内人亡政息的忧虑远远高於体制外异议力量渴望共产党下台的情绪。这是中共统治大陆六十多年来第一次出现的思想动向。<br />
<br />
