“鉴於民间对民主政治的诉求日高一日的实情，当局怕引起新一波的政治抗议，因此没有删除顾的网络文章。当然，有分析家认为这可能是北京最高决策层为两会营造 “民主气氛”而安排的一场政治戏剧。”<br />
<br />
“鉴於 。。。。当局怕引起”，他是当局么？他怎么知道当局没有删除文章的原因？文章还在的原因可以有很多，负责收集信息的部门没看到；负责上报的部门没上报；上报之后上层没要求删。这些都可能。到底为何还在，只有一层层采访了才知道。但很明显，这是采访不到的。于是大笔一挥，就“鉴於 。。。。当局怕“。如此行文，佩服。<br />
<br />
还有比如“在体制内最有影响的杂志《炎黄春秋》”<br />
体制内最有影响力的杂志是《求是》。《炎黄春秋》在体制内是另类，差点都被关掉了。<br />
一般来说，“最”后面的文字如果自己不是很确定，就把“之一”两字加上，尤其是涉及到像影响力这些不是很容易量化的抽象名词。把话说得那么死，等着被笑话。<br />
<br />
这种文章只能用粗制滥造来形容，看多了脑子会坏掉的。</div>

							
							
							
							
