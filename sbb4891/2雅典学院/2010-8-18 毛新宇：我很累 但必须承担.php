													<h2>毛新宇：我很累 但必须承担</h2>
						
						
																											<div id="postmessage_787117" class="t_msgfont">少将毛新宇首次接受平面媒体专访 称人生最大转变发生在参军以后<br />
<br />
　　毛新宇出生于1970年，是毛泽东次子毛岸青之子，是毛泽东唯一的孙子。先后毕业于中国人民大学、中央党校和军事科学院，获博士学位，2008年7月，被任命为军事科学院战略部副部长，副军级。 <br />
<br />
　　八一前夕，毛新宇晋升少将，成为中国首位70后少将。近日，早报记者在北京对他进行了专访，这也是他晋升少将后首次接受平面媒体专访。在记者眼中，毛新宇非常朴实，没有架子，同时也是一个十分严谨的人，采访开始前一定要再看一次采访提纲，细细琢磨后才称：“好的，我清楚了……” <br />
<br />
<img src="https://yopic.us/images/6341769821.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
毛新宇被授予少将军衔后在天安门留影。<br />
<br />
　　（作为领袖的后代，）我没有失去什么。有些东西可能在有些人看来是失去了，但我并不认为。可能现在随着社会的发展，有人把经商成功与否、能赚多少钱作为体现人生价值和生活质量好坏的标准。但说老实话，包括我父母这一代，我们都没有把金钱多少作为价值和生活质量好坏的标准。<br />
<br />
　　要承认一个客观事实<br />
<br />
　　东方早报：近年来社会对80后、90后的关注度颇高，而且有一个很有趣的规律，比如说对80后，一开始总是贬抑占主流，认为他们是垮掉的一代，但经过汶川地震、奥运会等大事件，80后又被看作是有担当、有责任心的，现在轮到90后了，上海世博会上很多90后志愿者的事迹非常感人。您对社会对80后、90后这种态度的转变怎么看？<br />
<br />
　　毛新宇：关于80后、90后这群当代青年人的主体看法，首先必须承认，现在的80后、90后，与我们70后、60后出生的人的想法和观念是不一样的。这是一个客观的事实。以我们家族来讲，我是1970年生人，我的兄弟姐妹都是出生于上世纪60年代，如果你要让我结合我的成长经历来讲，由于我出生在一个特殊的年代，成长的家庭环境又与很多人不一样，那是一个革命家庭、一个领袖家庭。但从总体上看，随着社会、时代的发展进步，现在80后、90后主流的发展方向应该是肯定的、不错的。<br />
<br />
　　东方早报：跟您出生的那个年代有什么不一样呢？<br />
<br />
　　毛新宇：今天这个时代，有两个明显的特点：一是党和国家的工作重心是建设社会主义市场经济；二是现在是知识经济、科技大爆炸的时代，而且以科技进步为特征，我们正式迈入信息时代。可以说80后、90后，包括我们这些70后，各自都有优缺点。以80后、90后为例，首先要肯定他们顺应时代的潮流，掌握信息、技术、新知识的能力比较强。缺点我就强调一条，我觉得他们尤其欠缺对党的历史的了解，特别是对共和国历史的了解，希望通过教育，能够在青年一代身上坚持和弘扬革命的优良传统和党的优良传统，比如勤俭节约、上世纪五六十年代良好的社会风气、人与人之间互相帮助、团结友爱等等。<br />
<br />
　　东方早报：那您觉得这种“欠缺”是教育的问题，还是社会新趋势导致的？<br />
<br />
　　毛新宇：这有多方面的因素，原因很复杂。我作为全国政协委员，每年我也写提案，提出希望我们国家重视对青少年传统革命教育。我觉得教育部门在这方面有这个责任。不管时代怎么发展变化，作为我们党的光荣历史、优良革命传统，什么时候都不能丢、不能忘记。<br />
<br />
　　参军是人生的重大转变<br />
<br />
　　东方早报：您出生在一个特殊的家庭，这决定了您不可能拥有与普通青年人一样的人生，您是从什么时候开始意识到自己比同龄人特殊的？<br />
<br />
　　毛新宇：大概从我上小学的时候吧，我发现自己比同龄的孩子更多地受到革命传统的熏陶和教育，所以我为什么后来读大学以后很快地就去学党史，又很快地去部队研究毛主席的军事思想，因为我对这些历史是比较熟悉的。<br />
<br />
　　东方早报：作为红色家庭后代，您对成长、成才有什么感触？<br />
<br />
　　毛新宇：首先还是我强调的，人生成长发展的道路是多种多样的，尤其是信息和网络时代，现在只要你有本事、有才能，社会给你提供的机遇还是很多的。你说的这个问题我讲两点：第一，在发展经济的时代，首先自己要有正确的人生信仰。第二，现在的社会发展给每个人的成长道路提供很多机遇，一个看你自己能不能抓住机遇，另外，特别要注重对个人道德思想品质的培养，不管将来如何发展，至少要做一个遵守社会公德的、对社会有益无害的人，而不能做犯罪、对社会产生危害的事情。<br />
<br />
　　东方早报：有学者指出，不能让青年失去努力向上的信念……<br />
<br />
　　毛新宇：这对啊。人活在这个世界上一定得有奋斗目标嘛。有些事情要加强引导和教育。有时候学校和家庭都有一个重要职责和义务，教育青少年怎样认识今天的社会现状，首先要认清我们所处的是怎样的一个历史阶段，另外，当然现在社会上存在一些不好的现象，比如贪污腐败等，这就要求正确认识这些社会不良现象。我觉得不管怎么样，总体的社会发展是好的，这些不良现象毕竟是少数，这就要靠学校和家庭正确的教育引导、正确的评价。<br />
<br />
　　东方早报：据说您的人生选择是您母亲给您定的方向，当时您自己是怎么想的？<br />
<br />
　　毛新宇：不能说是母亲帮我定的，只能说是母亲启发教育我选择走了一条正确的人生道路。说老实话，当时大学读历史，我业余时间也学了党史，只不过当时还是一种迷惘的心态，一切只凭我自己的兴趣爱好出发，在读大学期间还没有完全自觉意识到学党史和学毛泽东思想的重要性。我为什么说我那么热爱我的母亲呢，因为她是我人生的导师，是她使我意识到今后应该怎么样成长，走哪条道路，承担怎样的历史使命，这才有了我今天的发展。<br />
<br />
　　东方早报：您那么年轻就成为了少将，当初怎么会又进部队的呢？<br />
<br />
　　毛新宇：我曾经说过我人生三大里程碑，其中，我觉得成长最快、进步最大的就是入伍参军。当然我不是鼓励、提倡所有青年人都去当兵，但我认为军队确实是一个大学校。我人生最大的转变就发生在参军以后。2000年进军事科学院，入伍参军，读博士。以我这10年的工作学习研究体会，要认识和认清这个社会，这是一个非常困难的事情。刚才你提到今天怎样引导青年人认识这个社会，我觉得人作为社会动物，人也好，社会也好，本身就是非常复杂的，要认清他、识别他很不容易。按辩证法来讲，凡事有一利必有一弊，我们只能选择扬长避短。<br />
<br />
　　东方早报：那么年轻就被授少将衔，今后有什么打算？<br />
<br />
　　毛新宇：这就是我要表态的，首先，当了军人是我人生的重大转变，那么授了我少将，我心里话，这意味着我将把我的后半生全部献给我们的军队和国防事业。做上将军，说老实话，可不要单单把它看成荣誉，这可是很大的责任和压力啊。这将军你是不好当的，干好也是不容易的。今后我只能是好好地为军队的发展服务吧。你要重视这么一个现象，这几年，很多高校的学生都愿意当兵了，我觉得这是好现象，至少说明他们对自己的人生有一个正确的选择，可能会慢慢的让更多青年人认识到军人的巨大价值和闪光点。所以我觉得人生的道路不管怎么选择，一是有正确的信仰和价值观，二是要使自己具备丰富的知识，使自己做一个道德情操高尚的人，这样才能对社会有所贡献。<br />
<br />
　　东方早报：您是后来才入伍参军的？<br />
<br />
　　毛新宇：我原来在中央党史研究室，那时我以为这辈子就在这个单位搞党史了，后来到了部队，当时说实话，我从地方干部到军队，一开始不适应，觉得纪律很严，要求很严，不想待，但后来我适应了。这就是我要给青年人的经验：一个人吧，不管你喜欢什么职业，都要尝试各种机会，不要拒绝尝试，要不断体验。尝试完了以后认真思考到底适合不适合我，确实不适合，可以调整，往往我们都是被迫而为的，抵触却不得不去从事，不妨试着接纳它，说不定在你尝试的过程中你的思想就会发生转变，甚至喜欢上它。<br />
<br />
　　打算9月参观上海世博<br />
<br />
　　东方早报：您平时的一言一行，媒体都比较关注您，您会不会觉得很累？因为您必须做得足够好，才能给所有爱您、关心您，以及各种心态的人一个交代……<br />
<br />
　　毛新宇：是很累，确实很累，研究工作很累，平时生活言行等也都有要求。但是我觉得我必须要承担，必须要接受，因为谁让你成为领袖的后代、成长在这个家庭呢？<br />
<br />
　　东方早报：可这也不是您可以选择的呀？<br />
<br />
　　毛新宇：是啊，所以我觉得这要分两方面说，一个是别人对我的要求比较高，一个是我自己给自己提的要求也很高。<br />
<br />
　　东方早报：您觉得出身的特殊性让您失去了什么？<br />
<br />
　　毛新宇：我觉得没有失去什么。怎么说呢，应该比一般的人作出更多的贡献。说句俗话，有些东西可能在有些人看来是失去了，但是我并不认为。可能现在随着社会的发展，有的人把经商成功与否、能赚多少钱作为体现人生价值和生活质量好坏的标准。但说老实话，不说从我这一代，包括我父母这一代，我们都没有把金钱多少作为价值和生活质量好坏的标准。<br />
<br />
　　东方早报：我说的是可以很自由自在到处走走看看……<br />
<br />
　　毛新宇：这方面倒是没有什么失去。其实有点遗憾，由于工作和客观的事情，在亲情方面，有时候跟家里人沟通机会少一些，另外时间支配不可能像一般人那样。作为我来说，别人想象不到我的社会活动之多。社会上很多大学聘请我去给青年学生讲课，这是我一个很大的社会责任和业余活动。去，就得对学生负责，得把好的知识奉献给人家，不可以不负责任的。除了讲课，更多的社会活动，比如全国各地纪念红军长征、有关毛主席的纪念活动，你不出席是不行的，我也觉得出席很有意义。其实我觉得参加这些活动，我不仅不抱怨，而且我把这些活动都作为加深学习研究毛泽东思想的一个机会。所以说，失去的可能是不像一般人可以那么自由地支配自己的时间，但是我觉得我做了很多有意义的事情。当然，面临挑战最大的，还是讲课，现在这个时代的学生，他们不满足灌堂式的讲课，他们需要跟你互动，不管你是名人、教授，你给他们讲什么，他们不是就能立刻接受并买账的，他们思想活跃，会提出很多意想不到的问题来挑战你。如果没有足够的准备和知识储备，还真回答不了。<br />
<br />
　　东方早报：平时您都看什么书？<br />
<br />
　　毛新宇：我跟我爷爷读书兴趣爱好完全一样，看得最多的是哲学书，主要看马克思主义哲学，还读一点黑格尔、康德，再有就是看我爷爷的哲学。体育运动也遗传我爷爷的，爬山、游泳、走路、打网球，我打网球还行。<br />
<br />
　　东方早报：您去上海看过世博会了吗？<br />
<br />
　　毛新宇：还没，打算9月份再去吧。我对上海印象很好，不过上次去还是六七年前了，上海的发展变化很大。我最喜欢东方明珠和浦东新区。</div>

							
							
							
							
