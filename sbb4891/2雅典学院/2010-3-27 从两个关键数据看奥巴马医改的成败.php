每年为防范医疗事故而滥用抗生素和进行化验等就滓搅圃に憷朔讯畹37%。 <br />
22%的浪费来自弄虚作假，每年有2,000亿美元为此买单，譬如虚假医疗证明、收取回扣等。<br />
办事低效及行政冗员带来18%的医疗资源浪费。 <br />
每年有500亿至1,000亿美元用于医疗事故，桌朔炎芏畹11%。<br />
每年用于糖尿病等可避免病症的医疗费用为300亿至500亿美元。<br />
