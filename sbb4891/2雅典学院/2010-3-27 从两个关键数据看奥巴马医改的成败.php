2009年，全美14家最大的医疗保险公司的总盈利是90亿美元，而当年全美的医疗开支是2.472兆美元。你有没有看错？没有！所有的这些医疗公司的盈利总和只占医疗开支的0.4%！<br />
<br />
 再看看根据路透社的研究报告，每年美国大概会浪费高达8500亿美元在种种医疗失误和行政费用上面，这占了每年医疗总开支的三分一。<br />
<br />
 从这两个数字，大家可以看到，奥巴马经常挂在嘴边的冷酷无情的保险公司根本不是问题，他们的收的保险费绝大部分是用于支付医生和医院送过来的账单，其中就包括那8500亿的浪费。尽管保险公司经常性的严格审查控制医生和医院的浮滥支出，他们的盈利仍然只有可怜的90亿。仅仅是整个医疗支出的零头。那么大部分的钱去了哪里？<br />
