一连串斩杀外国人的事屡屡得手，使尊攘派志士们兴奋不已。1860 年 9<br />
月 7 日，长州藩武土木户孝允、松岛刚藏和水户藩的西丸带刀等，在停泊于 江户湾品川的长州藩军舰“丙辰”号上秘密聚会，订立《成破之盟》。在这 份也称“丙辰号盟约”的文件中，双方约定：水户藩负责“破”，即率先破 除条约、实行攘夷；长州藩则负责“成”，即策应后援，处理事态；两藩的 共同目标是驱逐“夷狄”，扫除通敌的幕府奸吏。“丙辰号盟约”的订立， 意味着尊攘行动将在超越藩界的横向联合中扩大规模，激烈展开。此后，驻 日公使馆、特别是带头向日本施加压力的英国公使馆，成为尊攘派集中攻击 的目标。<br />
　　英国公使馆坐落在江户城的东禅寺。 1861 年 7 月 5 日，水户藩武士 有贺半弥等十几名尊攘派志士制造了袭击英国公使馆的第一次东禅寺事件。 是日，有贺等志士按照事先策划的行动方案，突然挥舞日本刀冲进东禅寺， 在“杀尽英国人”的狂吼声中，与幕府派来警卫东禅寺的 200 余名卫乓展开 激战。英国使馆人员手执步枪、手枪，拼命射击。经过一阵混战，2 名使馆 人员被杀伤， 20 余名幕府士兵非死即伤，前来袭击的志士或战死，或负重<br />
　　<br />
伤，或被逮捕，无一幸免。水户藩武士的凶悍勇猛之举，导源于水户学排外 色彩强烈的尊攘观念，即有贺等在偷袭东禅寺的《趣意书》中所谓：“我等 不能对夷狄污染神州袖手旁观，决心基于尊攘大义采取行动??这样一来， 将逐步奠定扫除夷狄的基础，以慰天皇圣虑和将军之心。倘能如此，则无上 荣幸，万死不辞！”①<br />
　　继第一次东禅寺事件之后， 1862 年 6 月 26 日，警卫东禅寺英国公使 馆的松本藩武士伊藤军兵卫与英国水兵发生冲突，在杀伤数名英国水兵后被 捕，次日自杀，造成第二次东禅寺事件。1863 年 1 月 31 日，长州藩武士高 杉晋作、久坂玄瑞、品川弥二郎、伊藤博文等十几人，乘夜潜入品川御殿山 英国公使馆的建筑工地。走在最前面的伊藤用木锯锯断木栅栏，高杉、久坂 等钻将过去，四处点火，将木结构的英国公使馆烧成一片废墟。这是两次东 禅寺事件之后，对英国公使馆的第三次袭击。尊攘派志士对驻日使馆的攻击， 在外国人当中引起一阵阵恐慌，使他们感到“日本是个令人提心吊胆、有生 命危险的国度”。 1862 年 8 月赴日担当英国公使馆译员的阿耐斯特・萨托 心有余悸他说：“我在赴任前还留在英国的时候，除考虑日本气候变化造成 的危险外，也不得不考虑被剑术高超的武士杀死的危险，所以我购买并携带 了大量的火药、子弹、雷管和连发手枪。”他哀叹道：“外国人走出居留地 地界时，人人佩带手枪，而且睡觉时也经常把手枪塞在枕下。因此，在当时， 大量的手枪畅销日本。”②<br />
尊王攘夷派志土采取激烈行动的一个重要目的，在于提高自身的政治地<br />
位，对国内重大问题取得发言权。然而，以幕藩领主、上层公卿为中心的公 武合体派阻碍着尊攘派志土政治上的过快显达。由于传统的藩意识的制约和 藩本位之下主君、家臣主从观念的束缚，尊攘派志士很难站在主君的对立面 采取对抗行动。因此，幕府高官或朝廷公卿往往成为他们展开攻击的目标， 在击灭“国贼”的口号下，实行所谓替天行道的“天诛”，构成尊王攘夷运 动的对内行动的重要内容。<br />
1862 年 2 月 13 日（旧历正月 15 日），以水户藩武士平山兵介为首的 6<br />
名志士刺杀老中安藤信行的“坂下门之变”，就是尊攘派志士对内实行尊王 攘夷的重大举动。安藤发起的和宫下嫁政治联姻，引起志士们的不满。关于 安藤派国学者U次郎调查废除天皇事例的传闻，更是火上浇油。志士们断定 安藤让和宫下嫁到江户，是企图将皇妹扣为人质，迫使天皇批准条约，若天 皇拒绝则废除其帝位。因此，当 1860 年 8 月幕府罢免反对和宫下嫁的议奏德 大寺公纯，10 月天皇以废约攘夷为条件同意出嫁皇妹，1861 年 11 月和宫前 往江户下嫁将军家茂，即以幕府为中心的公武合体运动热火朝天之时，在江 户郊外牙塾授业的字都宫藩武士大桥讷庵也在加紧鼓动门徒反击“形同禽兽 的兰学者”和“危害国脉的西洋流”，实行攘夷，痛击国贼，维持国体。与 此同时，大桥与平山等水户藩脱藩武士频频联系，筹划刺杀行动。大桥因计 划败露被捕，平山等 6 名志士遂不惜铤而走险。<br />
　　1862 年旧历正月 15 日是为上元佳节，届时旅居江户的诸藩大名循例入 城谒见将军，庆贺节日。是日上午 8 时，将军居城西九里传出咚咚的鼓声， 安藤装扮整齐，率随从离开西丸附近藩邸。一行人来到坂下门时，一名男子<br />
<br />
<br />
<br />
①<br />
<br />
② [日]小西四郎：前引书，第 246、248 页。<br />
<br />
手举诉状，直奔安藤的乘轿。行至近前。但见那人忽然从怀中拔出手枪，向 乘轿连连射击。枪声大作之间，从道路两旁冲来 5 名手执日本刀的武士，怒 吼着杀向乘轿。护卫安藤的 50 名卫士死战不退，与 6 名亡命徒般的刺客厮杀 在一起。拼死围攻乘轿的志士尽管刺伤了安藤，但毕竟寡不敌众，被卫士全 部杀死。混战中，安藤带伤赤足跳出乘轿，夺路而逃。身为老中，临阵逃跑， 使幕府的威信再次受到沉重打击。5 月安藤引咎辞职，6 月久世广周亦被罢 免。这样，前后存在不过两年的安藤一久世体制崩溃，幕府政权再度陷入迷 乱之中。<br />
　　“坂下门之变”的谋刺方式是“樱田门外事件”的再现，其刺激作用也 十分相似。尊攘派志士在搞垮了安藤一久世政权之后，又把攻击矛头指向宫 廷内部的公武合体派公卿。他们在实行“天诛”的叫嚷声中，对促成和宫下 嫁的“四好二嫔”（即公卿岩仓具视、千种有文、富小路敬直、久我建通及 嫔妃今城重子、堀河纪子）大加威胁。9 月迫使朝廷将他们逐出宫门，蛰居 在家或剃发修行。<br />
　　在此前后，暗杀“安政大狱”时迫害尊攘派志士的幕吏的复仇风潮，在 京都、江户等处掀起。 1862 年 8 月，撮合和宫下嫁政治联姻的关白九条尚 忠家士岛田左近，因充当过井伊直弼的帮凶、热中公武合体运动，被萨摩藩 武士田中新兵卫等斩杀，弃尸京都四条河原；9 月，捕吏文吉被暗杀，暴尸 京都三条河原；次年 2 月，曾奉安藤信行之命、调查废黜天皇先例的稿次郎 在江户和学讲谈所中被伊藤博文等志上暗杀。暗杀之风愈刮愈烈，甚至连早 已作古者竟也无法幸免。1863 年 4 月，尊王攘夷派志士三轮田纲一郎等闯进 祭祀室町幕府开创者足利尊氏的京都等持院，用刀砍下足利尊氏及足利义 诠、义满的木雕头像，弃之贺茂河原。<br />
在尊攘狂潮的裹挟之下，诸藩政治倾向急剧变化。在长州藩，1861 年 5<br />
月深得藩主毛利庆亲信任的直目付长井雅乐提出《航海远略策》，主张公武 合体，放弃废约攘夷而采用开国进取方针，远航五大洲，弘扬日本国威，压 倒全世界。毛利庆亲对长并的主张十分赏识，遂将这种以公武合体、开国进 取为中心的方针，确定为藩的基本政策。6 月，命长井游说于幕府与朝廷之 间，借以提高长州藩的发言权，扩大在全国的影响。由于长井在《航海远略 策》中提出的公武合体、废约攘夷不可取的主张与幕府政策一致，而弘扬国 威、压倒世界的目标又迎合了朝廷的虚荣心，结果，长井的游说相当成功， 长井本人也十分得意。<br />
然而，长井的公武合体论和开国说引起藩内尊攘派武士的忌恨，以久坂<br />
玄瑞、寺岛忠三郎、伊藤博文等为核心的尊攘掀发起弹劾长井的政潮，并在 暗中策划刺杀长井的行动汁划。藩内政局不稳，加之尊攘派在各地制造事端， 迫使藩主毛利庆亲于 1862 年 7 月下令长井回藩闭门思过。随后，毛利庆亲宣 布放弃公武合体的《航海远略策》，转而将尊王攘夷确定为举藩推行的基本 方针。尊攘派立即实行攘夷的主张占了上风，长州藩成为尊王攘夷的一大据<br />
点。<br />
　　在萨摩藩， 1858 年 8 月岛津齐彬病故，其弟久光的长子忠义继任藩王。 久光作为忠义的监护人，执掌实权。久光是一个顽固的攘夷论者，主张举藩 勤王，并以此为号召，增强萨摩藩对中央政局的发言分量。为此，他重用大 久保利通等尊攘派，解除对西乡隆盛的流放，调至藩厅任用；另一方面，久 光又是一个以雄藩为中心的公武合体论者，热衷于不改变现存统治体制的前<br />
　　<br />
提下，自上而下地开展幕政改革，厌恶尊攘派志士扰乱武土内部自藩主而重 臣而上级武士而中下级武士的等级序列、动辄犯上作乱、杀人放火的极端行 为，不惜以镇压手段来维护统治秩序。1862 年 5 月 14 日，久光为敦促幕府 实行雄藩大名分享政权、尊奉天皇朝廷的幕政改革，采取了前所未有的重大 举动，即亲率 1000 余名藩兵。北上京都。各地尊攘派志士闻讯雀跃不止，久 留来藩水天宫神社词官真木和泉、筑前藩武士平野国臣、庄内藩武士清河八 郎、长州藩武士久板玄瑞等紧急联络萨摩藩激进的尊攘派志士有马新七等 人，试图乘久光率兵进京之机，举行反幕府的暴动。</div>

							
							
							
							
