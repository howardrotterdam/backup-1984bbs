													<h2>长崎事件</h2>
						
						
																											<div id="postmessage_353318" class="t_msgfont"><a href="http://zh.wikipedia.org/wiki/%E9%95%BF%E5%B4%8E%E4%BA%8B%E4%BB%B6" target="_blank">http://zh.wikipedia.org/wiki/%E9%95%BF%E5%B4%8E%E4%BA%8B%E4%BB%B6</a><br />
长崎事件也称镇远骚动，是1886年（清光绪12年）在北洋水师造访日本长崎期间发生的一起外交事件。事件最终以双方的妥协而不是清廷的单方面退让收场，所以有人认为这是清廷在鸦片战争之后的第一次外交胜利[1]。<br />
<br />
缘起<br />
<br />
1886年7月，当时尚未正式成军的北洋水师，在李鸿章的命令下，抵达朝鲜元山一带巡弋操演，“以振国威”。后又奉命前往俄国海参崴访问。鉴于铁甲舰在作长途航行后，需回港涂油维护，而当时清廷的旅顺军港尚未完工，李鸿章遂决定由水师提督丁汝昌率镇远、定远、威远、济远四舰前往位于日本长崎的三菱造船所进行检修，并展开对日本的亲善访问。这也是中国铁甲舰队的首次访问日本。<br />
<br />
经过<br />
<br />
1886年8月13日，日方邀请抵达长崎的北洋官兵登岸购物。一些水兵却违反军纪，前往当地妓院寻乐且酗酒斗殴。闻讯赶来的警察很快平息了事端，多名情绪激动的清兵进而前往警署继续闹事。期间以军刀将一名日本警察刺成重伤，肇事水兵也受了轻伤并被逮捕。事后李鸿章对于北洋水兵的违纪行为采取姑息的态度，他说，“争杀肇自妓楼，约束之疏，万无可辞”，“弁兵登岸为狭邪游生事，亦系恒情。即为统将约束不严，尚非不可当之重咎，自不必过为急饰也”。<br />
<br />
1886年8月15日，李鸿章授意全舰队放假一天，并允许450名水兵自由观光。虽然丁汝昌明令禁止水兵执军械上岸。然而由于两天前的纠纷所带来的不满，观光很快演变为了械斗。早有防备的警察数百名将水兵分割包围，配合街边市民展开石块攻击。事后统计显示，水兵方面有5人死亡、44人受伤、5人失踪（一说10人死亡）。警察也被打死1人，30人受伤。而长崎市民亦有多人受伤。<br />
<br />
冲突中，在长崎市同时出现了短暂的排华浪潮，许多已入日籍的华人遭到牵连。<br />
<br />
 结局<br />
<br />
与以往处理外交事件不同的是，清廷表现出了相当强硬的态度。北洋水师总教习琅威理甚至提出了对日断交、武力解决的主张。李鸿章的外交顾问伍廷芳被认为在此事的圆满解决上起了关键作用。<br />
<br />
1887年2月，双方达成彼此让步的协议，双方一致认定是语言不通导致了误会的发生，所以肇事者交由本国政府自行处置，而死伤人员则由对方予以抚恤。值得一提的是，日方所支付的抚恤金数额大大超出了清廷。</div>

							
							
							
							
