<br />
苏明：“邓小平则是在身处宪政民主的政体之下，全盘照搬了西方马列，颠覆了民主自由，复辟了极权专制，又在宪政、民主、人权、法治成为了普世价值的情况之下，又提出了绝不照搬西方。”<br />
<br />
有人说：“邓小平解决了中国人的温饱问题”。<br />
<br />
顾晓军认为，世界在前M，即使没有邓小平思想，无论是由谁领导，十几亿中国人，也绝不会被饿死、冻死。但是，这30年来，从教育、医疗、住房、分配与收入及养老、国企改革、农民土地等问题越演越烈，深刻反映了“邓式改革”是“誓与人民为敌”的改革方式。<br />
<br />
