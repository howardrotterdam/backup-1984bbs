													<h2>三批邓 ：邓式改革是誓与人民为敌</h2>
						
						
																											<div id="postmessage_684778" class="t_msgfont">作者：顾晓军 2010-06-06 06:56:10 发表于：博客中国<br />
<br />
<br />
顾晓军主义：评论中国・之六百三十八<br />
<br />
　　无产阶级革命家邓&nbsp; &nbsp;，一生为中国人民鞠躬尽瘁、死而后已。<br />
<br />
　　也许，没有人会想到：中国改革开放的总设计师邓 ，在他逝世13年之后；会被我顾晓军，定性为DD“誓与人民为敌”。<br />
<br />
　　于此，我顾晓军，一个小时之前，也真的没有想到。但，此刻，我想到了。请大家耐心看，我试论之。<br />
<br />
　<br />
<br />
　　为什么说总设计师邓小平是“誓与人民为敌”呢？<br />
<br />
　　早在1979年3月30日，邓小平就在北京理论工作务虚会上提出了“坚持四项基本原则”。这“四项基本原则”，邓是把它当作中国“实现四个现代化的根本前提”提出来的。也就是说，他认为：如果违反这“四项基本原则”，中国宁可不要“四个现代化”。是不是这样？这是其一。<br />
<br />
　　其二，“四项基本原则”，针对的是当时的“宣扬无政府主义和资产阶级自由化的主张，反对社会主义制度，反对共 的领导，反对无&nbsp; &nbsp;专政的政权，反对毛 思想的指导地位”等。这就不难理解，十年后，邓 敢于下令扼杀中国的民主实践。<br />
<br />
　　在邓 之前，在中国的现代史上，敢于下令扼杀民主实践的，只有两个伟人，一个是蒋 ，一个是毛 。邓 ，这一步就跨入了中国伟人的行列。<br />
<br />
　<br />
<br />
　　纵观世界历史，任何时代、任何国家的改革，都是政治、经济、社会、司法及文化等诸方面的改革相互关连、相继展开的。<br />
<br />
　　在中国，因为有总设计师定了调子；从而，中国的改革开放，从此就拒绝政治改革，只能在经济及其他层面上做文章，头痛医头、脚痛医脚，手痛却在肚子或屁股上扎针，使这场关乎中国命运与前途、关乎全民生计的改革，成了一场扭曲的、畸形的、T型台上的政治作秀，成了民主、人权与完善收入分配格局等竟要美国人来为我们争取的闹剧。<br />
<br />
　<br />
<br />
　　然而，单从政治上提出批评，不足以支撑我顾晓军之“邓式改革是誓与人民为敌”的命题。<br />
<br />
　　那么，就请大家跟随我的笔触，看一看改革开放这30年的伟大实践，是不是在实践着“誓与人民为敌”。<br />
<br />
　<br />
<br />
　　有人跟贴说：“邓 解决了中国人的温饱问题”。这是胡扯蛋！是强词夺理。<br />
<br />
　　整个世界在进步，中国不会作壁上观。改革开放这30年，如果没有邓 思想，无论是由谁领导，十几亿中国人，也绝不会被饿死、冻死。<br />
<br />
　　相反，邓 的“让一部分人先富起来”、“发展才是硬道理”及“猫论”、“摸论”等等，却破坏了当时在中国已经建立起来的“毛式公平”。“毛式公平”不是真正意义上的社会公平。但，正确的做法是：从政治上推翻其理论，从经济上实行改革，而从社会等层面修补当时中国的现实。然，邓小平的做法，却恰恰相反。他以“四项基本原则”，在政治上予以最大的庇护；而在经济、社会及其他层面上，却以最大的努力从事着最大力度的破坏。<br />
<br />
　　近日，我已写下了《邓 思想批判：让一部分人先富起来》、《邓 思想再批判：发展不是硬道理》。今天，就不再去纠缠他的“猫论”、“摸论”及其他了。我仅以民生的几个大问题，展开“邓式改革”是“誓与人民为敌”的这一命题，并对其进行批判。<br />
<br />
　<br />
<br />
　　改革开放之初，中国的义务教育，基本上已形成。尽管很不象样，尤其是教学设施破烂不堪。但，改革开放这30年，却破坏了原有的“毛式公平”的教育体系。分班制、补习制、收费制及贵族学校等兴起、林立，使优质教育资源向富人倾斜，至使穷人上不起学，入学也难呆下去。<br />
<br />
　　慧慧的妈妈，送女儿上学去的最后一句话，是“你上学去吧，妈妈马上要走了……”。农民谢光福，为了儿子6000多元的学费，永远地累死了。还有那一个个熟悉而又陌生的、曾触动过我们的名字：杨元元、马加爵等。<br />
<br />
　　而如今，义务教育、公办教育、免费教育，已成为国际上的惯例，并非西方发达国家的“专利”。俄罗斯已做到了从幼稚园到大学，全部免费。即便是那少数时常找我们中国要钱、要粮的国家，也是免费教育。而中国，为什么要让人民失去享受义务教育的权力呢？从这点上说，难道“邓式改革”不是“誓与人民为敌”吗？<br />
<br />
　<br />
<br />
　　近日，我转发了《陕西神木县委书记称免费医疗让政府大赚》。神木县的做法，实际上就是过去“毛式公平”中的“公费医疗”；如今，却当作改革的试点。<br />
<br />
　　神木县委书记郭宝成在总结时说：“公务员没吃亏，占了点小便宜，农民占了大便宜。谁吃亏了？唯一吃亏的是收红包的医生。实际上，医生现在收入明显增加了，红包少了。政策不管住红包，社会就没有公平正义”。<br />
<br />
<br />
　　其实，免费医疗已是国际大趋势。同为人口大国的印度，现在实行的就是免费医疗。<br />
<br />
<br />
　　而“邓式改革”，却放弃公平、正义，打破“公费医疗”秩序，搞患者自理、医疗保险；院长负责、科室承包，提高药价、处方红包等。一切与效益挂钩，把社会公益事业，当作赚钱的机器。<br />
<br />
　<br />
<br />
　　住房已是现今中国的痛。<br />
<br />
<br />
　　“毛式公平”中的福利房，是中国城市人以几十年低工资为代价换取的。住房改革，竟然作价卖给大家，作最后的掠夺。如是，也没有房产的土地权，一旦遇上拆迁，便成为任人宰割的羔羊。而这还是幸运的，那房改前没有享受到福利房的，只有自掏血汗钱。在农村，屋是自己的，而屋下的土地，却是别人的。<br />
<br />
<br />
　　房改后，兴起的是房地产业。一个原本没有的产业，通过特殊准入、捆绑银行资金（滋生大量腐败），在短短的十几年间，实现了绑架中国经济命运与十三亿人民生的现实。且谁也管不了DD谁砸他们的饭碗，他们能砸全中国的饭碗。而国际惯例，是实行严格的差别化的房产税，有效禁止和打击炒房投机。中国，却迟迟不肯下手。为什么？因房地产业，是官僚资本的密集区。<br />
<br />
<br />
　　中国人民奋斗几十年，落得个居无定所。这难道不是“邓式改革是誓与人民为敌”吗？<br />
<br />
　<br />
<br />
　　今日，分配、收入与养老，已人为地把十三亿中国人划分成了不同的等级。<br />
<br />
<br />
　　在“毛式公平”中，毛 月薪约为数百元。那时的13级高干，130元左右；老百姓一般数十元，八级工能有100左右。如今呢？先搞企业化管理、社保制。大量的普通工、老工人，在1000元左右，且多年没有太大变化。而后，搞公务员制、“高薪养廉”，再偷偷摸摸地完成公务员养老改革。如今，一个正处级公务员或相当于公务员退休后，月收入都可以在万元以上。<br />
<br />
<br />
　　所以，在转发《三部门:按家庭成员拥有住房数认定二套房》时，我毫不客气地说：“老百姓‘不要你认定，你给我们滚下台’。如同前日人保部的发言：工资要翻番。其必是普遍翻番，拿一万的成两万，拿一千的只两千，物价翻番带拐弯，老百姓不是更苦吗？”<br />
<br />
<br />
　　以上说得还是劳动报酬，不含资本回报、灰色收入等。<br />
<br />
<br />
　<br />
<br />
　　国企改革，是“邓式改革”“誓与人民为敌”的重灾区。<br />
<br />
<br />
　　优化组合，让“工人阶级是社会主义中国的领导阶级”，成为一句空话、屁话。大量的工人，被权力“优化组合”后失业，还美名其曰为下岗。而后，是经营权的承包、出租；再，是国有企业的私有化。<br />
<br />
<br />
　　而国企私有化的过程，不过是经营者捆绑银行资金。其中的贿赂、腐败，且不去说。如果行业周期向好，则罢；若正巧赶上行业周期性萧条，则大量产生呆坏帐，这亦由银行即国家来消化。而以权贵华丽转身的私营企业主，却没有多大风险。<br />
<br />
<br />
　　而近十几年，数千万下岗工人，是怎么过呢？不少人，都深有体会吧？就不需要我顾晓军再说了。<br />
<br />
　<br />
<br />
<br />
　　多少年来，中国农民是最庞大的弱势群体。所以，他们不怕流血牺牲，最真诚地跟着毛泽东打江山。然而，他们在“毛式公平”中，没有得到什么好处，得到的是以农养工、城乡差别、农业户口等。<br />
<br />
<br />
　　“邓式改革”，也不是没有伟大之处。其最亮丽的一笔，是土地承包。然而，邓 的思想，在“四项基本原则”中打转、裹足不前。换句话说：中国农民，跟着毛 、邓 奋斗了几十年，依旧没有自己的土地；甚至，连宅基地都不是自己的。<br />
<br />
<br />
　　中国农民只落得红头文件的承诺、若干年不变，而不是真正的合法。<br />
<br />
　<br />
<br />
<br />
　　以上，从政治改革与民生的教育、医疗、住房、分配与收入及养老、国企改革、农民土地等问题，阐述了“邓式改革”是“誓与人民为敌”的改革方式。<br />
<br />
<br />
　　中国的改革，已走到了十字路口。犹豫、彷徨，无济于事。走回头路，更不是办法。中国，需要戈尔巴乔夫式的勇气、蒋经国式的清醒。而我顾晓军，最多相当于狗屎李敖，啥也做不到。我惭愧自己没有能力。<br />
<br />
<br />
　　在此，我向我深爱的祖国和中国老百姓，作最深刻的检讨。并希望我对邓小平思想的系列批判，能引发中国思想、政治、经济等各界的全社会大讨论。因为，不触及邓小平的思想，中国的政治改革，无从谈起；中国经济改革的对与错、优与劣、好与坏，也都没有办法谈。<br />
<br />
　<br />
<br />
<br />
　　于本文，我顾晓军声明：批判从严，“处理”从宽。无论如何，邓小平都将是中国现代史上的伟人之一。<br />
<br />
<br />
　<br />
<br />
