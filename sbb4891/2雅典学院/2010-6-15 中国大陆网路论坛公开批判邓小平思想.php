													<h2>邓思想再批判：发展不是硬道理</h2>
						
						
																											<div id="postmessage_684773" class="t_msgfont">发展不是硬道理。<br />
<br />
　　公元2010-6-3～4，我顾晓军，以此展开对邓丨防和谐丨小平思想的再批判。<br />
<br />
　<br />
<br />
　　为什么我要说“发展不是硬道理”呢？因为，“发展才是硬道理”，是邓思想中的重要组成部分。而邓自己，大约也没有想到：在“发展才是硬道理”的幌子下，中国的官僚资本，肆无忌惮地对中国的老百姓进行着压榨。所以，如果我不在这里拿“发展才是硬道理”“祭旗”，中国的老百姓，就甭想摆脱中国官僚资本的理论的压迫。<br />
<br />
　　且，就“发展才是硬道理”之本身来说，也是错误的。<br />
<br />
　<br />
<br />
　　从句面来上看，“发展才是硬道理”，是没有什么错的。不发展怎么能行？是不是？大家都会懂这么一个浅显的道理。然而，往往正是这类浅显的道理，掩盖着一些不为人知的、深藏的谬误。<br />
<br />
　　那么，“发展才是硬道理”究竟错在哪里呢？其错，首先在于我们的理解DD通常，我们以为：“发展才是硬道理”，是表述。其实不然，“发展才是硬道理”是强调。而强调，就有它的潜台词。“发展才是硬道理”的潜台词是DD追求高速发展。如果不是要追求高速发展，就没有必要强调。是不是这样？<br />
<br />
　<br />
<br />
　　以上，我是用语言分析，破了“发展才是硬道理”的局。只要留意，在我们的身边，这样的谬误还很多。它们的共同点是：从句面上看，似乎没有错；但，无论是谁，只要你认真地去理解，就必然误入歧途，且是不知不觉的。<br />
<br />
　　而事实上，在邓思想的深处，也确实就是这么想的。不知大家还记得否？当时，有一句话，叫作“深圳速度”。<br />
<br />
　<br />
<br />
　　如是，我顾晓军就要准备批判DD“追求高速发展”了。<br />
<br />
　　注意：这不是我在公然偷换概念，而是“发展才是硬道理”之本身，早已偷换了概念，并使一般读者无从识破。当然，我也不是说邓先生有意制造了这么一个文字迷宫，我甚至相信他没有这么个“设局”的功夫。但，高人就是高人，在有意无意间，一个旷世句迷就诞生了。<br />
<br />
　<br />
<br />
　　下面，我着重解决：深藏在“发展才是硬道理”中的“追求高速发展”。<br />
<br />
　　“追求高速发展”，简单地看，也没有错。发展的快，难道不好吗？一般的理解，是发展得越快越好。其实，这是大错特错的，也是50多年前那场“大跃进”的错误的思想根源。<br />
<br />
　<br />
<br />
　　翻开人类社会的近代史，一如“大跃进”的错误与后果，比比皆是。<br />
<br />
　　二十世纪之初的美国，经济高速发展、惊人的繁荣。那时，冰箱、洗衣机、轿车等在今天中国人看来仍是奢侈品的东西，就已经进入到一些普通的美国家庭。然而，与“追求高速发展”如影随形的，是贫富差距、是超前消费、是投机……1929年的股灾，就如约而至，狠狠地惩罚了“追求高速发展”的美国。紧随股灾的经济大萧条，使美国经济倒退了2丨防和谐丨0年。<br />
<br />
　<br />
<br />
　　我顾晓军，有责任告诉大家：“追求高速发展”，是有害的。“发展才是硬道理”，是看似有道理的没有道理。<br />
<br />
　　德国，也曾在希特勒的领导下“追求高效发展”；而后，又被他拖入战争，毁掉了那个德国。这样的“追求高速发展”，对于国民来说，有什么意义呢？前苏联，也曾辉煌过。于是，便大打冷战。结果呢？导致苏东事变。当然，苏东事变是有深层的政治原因的。但，那个时代，经济是基础；经济输了，一输百输。反过来说，前苏联在经济上的输，也输在政治理念上、输在人为的主义欲战胜自然的经济上，更输在“比赛”上。<br />
<br />
　<br />
<br />
　　至此，我们基本上可以认定：“追求高速发展”，没有意义。同样，“发展才是硬道理”，也没道理。那么，在邓思想中，为什么会出现“发展才是硬道理”呢？<br />
<br />
　　邓丨防和谐丨小平先生，是无产阶级革命家，他不可能不以无产阶级革命事业为重。换句话说，“发展才是硬道理”的提出，主要是兼顾党国与军国的利益，是要抓住冷战结束后、又不可能有大的世界战争的这几十年的机会，“追求高速发展”，做好准备，迎接未来的战争。<br />
<br />
　<br />
<br />
　　以上，是我对邓思想的“发展才是硬道理”的根源追寻。这，也是邓丨防和谐丨小平这一思想的错误根源。<br />
<br />
　　邓，长期生活在毛思想的时代。或者说，邓思想，是在毛思想的影子下形成的。所以，尽管邓也很伟大，从纷乱的世界局势中能认识到暂时没有世界大战、可以大搞经济；但，他没有能完全抛弃“时刻准备打仗”的理念。可以说，邓搞经济的目的，还是为了准备打仗。<br />
<br />
　<br />
<br />
　　而我顾晓军认为：由于信息时代的到来，除了局部战争，已不可能再有大规模的世界大战。道理很简单，全世界的主要人口，已很难再被某种主义蛊惑、为少数人欺骗而进行绝死大博斗。军事战争的形式，将被经济战、经济谋略、经济大战略思考等所取代。人类社会，将从比国土的面积大、比国家的强盛度等，变为比民众的自由、民丨防和谐丨主与幸福。<br />
<br />
　　因此，毛、邓等革命家为中国打下的经济基础，是功不可没的。但，毛、邓思想过时了。因为，他们的共同点是：抓经济的目的，是搞建设，并准备打仗。而今后，抓经济的唯一目的，应该只有两个字：民生。<br />
<br />
　<br />
<br />
　　从民生的角度出发，就不需要强调“发展才是硬道理”，更不能“追求高速发展”。前面，已经举了二十世纪之初的美国的例子。其实，二十世纪七十年代的日本，也是“追求高速发展”的牺牲品。日本，曾富得梦想买下整个美国。结果呢？到现在仍一蹶不振。<br />
<br />
　　我顾晓军认为：经济发展，就是经济发展，不要想着打仗，不考虑到给谁看；经济发展，不是“建设”，更不是无聊的“鸡的屁”。经济发展的全部目的，是惠民、为了老百姓的民生。这样的经济，才是健康的，“速度”也自然会在其中。<br />
<br />
　<br />
<br />
　　苏东事变后，我们曾笑话过俄罗斯。可，人家现在DD从幼稚园到大学，全部免费；房产无偿私有化，人均18平米内由国家赠送；急病，不用预付押金；国家税收的三分之一，用于购买公共品（详见《告诉你：今日真实的俄罗斯与改革》）。东欧转型的国家，斯洛文尼亚、捷克、匈牙利已经进入发达国家行列，爱沙尼亚、斯洛伐克、克罗地亚、立陶宛、波兰、拉脱维亚等，亦已经加入了发达国家的预备梯队（详见《难怪好几年都没有这些国家的新闻和消息》）。<br />
<br />
　　而我们呢？前一时，温总说了：“中国实现现代化还需上百年”。<br />
<br />
　<br />
<br />
　　发展不是硬道理！民生才是硬道理！<br />
<br />
　　前些年，印度也很繁荣。结果，在大选中，印度人民把带领印度进入繁荣期的人民党选下了台。印度人民很蠢吗？不，印度人民需要乡村与都市一样的繁荣，而不需要畸形的繁荣。<br />
<br />
　<br />
<br />
　　中国，没有大选形式。所以，喜欢打肿脸充胖子的政丨防和谐丨府，是不会下台的。但，矛盾便以其他形式表现出来，如：遍地自丨防和谐丨焚、征地血案、滥杀学童、冤假错案……等等。为了“发展才是硬道理”，中国老百姓付出了极其惨重的代价。<br />
<br />
　　就思想而论，“发展才是硬道理”，是以左的政治面孔出现，行极右的经济路线与战略；是以左，掩盖其极右的本质。趁人们一时认识不清、把握不住，官僚资本便疯狂掠夺，腐败亦悄然蔓延。如，以砸“大锅饭”为借口，使数千万工人失业，美其名曰：下岗。以“改革”、“发展才是硬道理”为借口，取消单位福利分房、取消公费医疗、取消退休金制度等。然而，权力部门自己，却依旧在暗地里搞超低价的福利式购房。<br />
<br />
　<br />
<br />
　　老百姓的实实在在利益没有了，老百姓还会怕死吗？正如在被强拆中自丨防和谐丨焚的唐福珍所言：“如果失去了苦心经营十多年的家，活着也没了意义……”<br />
<br />
　　这，是“发展才是硬道理”对中国社会的最大破坏，也是我冒死写这篇《邓丨防和谐丨小平思想再批判：发展不是硬道理》的根本所在。<br />
<br />
　<br />
<br />
　　我希望：本文，能引发中国经济界、思想界的大论战，而不是我真的去死。<br />
<br />
　　但，即便是死，也没有什么可怕的。我，将会坦然面对。<br />
