													<h2>倪玉兰 - 维基百科，自由的百科全书</h2>
						
						
																											<div id="postmessage_664414" class="t_msgfont">倪玉兰<br />
维基百科，自由的百科全书<br />
<br />
<img src="http://thumbsnap.com/i/PBlje0Aa.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
倪玉兰（1960年3月24日－），北京人，北京语言学院文学学士、中国政法大学法学学士。中国女律师、维权者。 <br />
<br />
[编辑] 简介<br />
1986年到2001年，倪玉兰在律师事务工作。<br />
<br />
2002年4月27日上午，倪玉兰因拍摄北京西城区新街口大四条55号强拆现场，而被警察拉到新街口派出所殴打，致其“大小便失禁、多次昏迷”。后警方以倪玉兰“阻碍办公秩序”为由，要求将其行政拘留10 天。7月3日，西城公安分局以倪玉兰“涉嫌妨害公务罪”向西城检察院提请逮捕。7月10日，西城检察院将案卷退回西城公安分局，当天倪春兰才得以回家，期间被拘留75天。随后倪玉兰开始上访。9月27日，倪玉兰试图向北京市人大常委会反应冤情，被新街口派出所民警刘俊杰、闫修建殴打，致其摔断尾骨，并将其关押。11月14日，西城区人民检察院对倪玉兰提起公诉。11月27日，西城区法院以“妨害公务罪”判处倪玉兰有期徒刑一年，其律师资格被吊销。狱中，她得到了法轮功狱友的照顾，但因为伤势没有得到救治，此后落下残疾，双腿无法行走，生活已不能自理。[1][2][3] 2003年7月12日，倪玉兰获释。此后继续上访维权，也继续受到警方的打压，根据她的陈述，2003年10月15日她又被警察李楠等人抓进新街口派出所“非法拘禁”三天共50个小时；从2004年7月27日至2006年3月16日，她失去人身自由长达597天。[3]<br />
<br />
2008年4月15日，因倪玉兰家西城区前章胡同19号面临强拆，[4] 后西城区人民检察院认为，因拆迁施工纠纷，倪玉兰“暴力阻碍工人施工”，致使工人“受轻微伤”，此后倪玉兰被民警带至新街口派出所接受调查，倪玉兰“不服从民警管理，踢打民警肖巍下体，致使其睾丸挫伤，被当场抓获”，而以“妨害公务罪”起诉倪玉兰。而根据倪玉兰的陈述，4月15日她被“塞进汽车后备箱中，拉到新街口派出所，关押在小黑屋内”，并遭到“多次刑讯、毒打”。倪玉兰的律师胡啸在探视倪玉兰后，告诉记者，“她确实身体非常不方便，可以说一步一步挪着走，拄着双拐，身体瘦弱，非常苍白。从我看见她从楼下走到我身边的时候，大约有半个小时了。”[5]11月，倪玉兰家被强拆。[4]12月18日，北京市西城区人民法院以“妨害公务罪”，判处倪玉兰有期徒刑二年。[6]倪玉兰的另一位辩护律师李方平表示，法院“突然宣布开审，在没有辩护律师，o有一名家人获准旁听的情况下进行，而公诉方指控倪玉兰打警察的重要证据，现场的录像竟未有当庭播出，所有证人亦没有出庭接受质询，开庭两日后就迅速作出一审判决，律师多次到法院查阅档案遭拒”。法院拒绝二审开庭，2009年3月，律师收到维持原判的书面通知。[7][8][9]另据媒体报道，倪玉兰被关押后，九个月内一直不获准与家人见面。[10]倪玉兰丈夫董继勤将拆迁相关案件通过网上举报中心举报至中国最高人民检察院。[11]<br />
<br />
2010年4月14日，倪玉兰出狱，当天她接受媒体采访，陈述其在狱中的悲惨状况，“我有拐他们不让我用，扶着凳子也不行，每天让我在地上爬行，每天还让我到车间去干活，装一次性的筷子，从早到晚不停的干”，“我每天从五层的楼上往下爬，爬到楼下，再爬五百多米到车间，再从楼下爬到楼上，这种每天要爬四个来回，算起来一天要爬六个小时，正常人每次走十分钟的路，我要爬一个多小时才能到，天热的时候我是这样爬的，地非常的烫，我的手和腿全都烫出了大血泡，所有的辅助工具都不让我用”。“每天早晨别人到外面做操，我就到大厅里撅着，就跟文化大革命时知识分子被批斗的时候一样，四肢撑在地上，有的时候我的腰根本撑不了，她们就弄两个人来监视我，一看不行了，就把我提了起来，出操的人回来后要让他们看我，说谁像我一样不认罪就会遭到同样的惩罚”。“就是我上厕所他们都不让我上，我上去了，进去了，把裤子扒下来往下蹲，结果他们看到我蹲就强迫把我拖出去，不让我解手，说是队长不让我解，他们从厕所里给我拖出去，我的牙撞到门板上掉了一半。我经常的被从厕所里拖出去，警官说这就是对你不认罪的惩罚。他们白天不让我们解大手，晚上也不让，因为他们经常在里面，我们喝不上水，就甭说要刷碗，洗脸，漱口了。”[12]<br />
<br />
因为房屋被强拆，倪玉兰和丈夫董继勤先是住与小旅馆，但因收到警方压力，现在两人只得露宿北京街头。[13][14]2010年5月7日，倪玉兰案的二审代理律师刘巍被北京司法局吊销律师证。[15]<br />
