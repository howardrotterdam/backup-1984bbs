													<h2>为阻止欧盟官员会见倪玉兰夫妇被关押</h2>
						
						
																											<div id="postmessage_664419" class="t_msgfont">为阻止欧盟官员会见倪玉兰夫妇被关押<br />
<br />
2010年5月27日 星期四<br />
<br />
遭受了狱中非人折磨后，又因住房遭强拆而被迫露宿街头的北京著名维权人士倪玉兰夫妇，5月27日凌晨被西城区警察强行带到厂桥派出所附近的一家旅馆关押，以阻止欧盟官员会见他们夫妇。<br />
<br />
倪玉兰透露，联合国考察团今天到北京皇城根调查采访，昨天晚上将近10点北京市公安局来了两名警察，要求他们离开在南河沿搭建的临时窝棚。<br />
