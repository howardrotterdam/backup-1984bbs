													<h2>倪匡：我不会回大陆</h2>
						
						
																											<div id="postmessage_263823" class="t_msgfont"><strong>倪匡：我不会回去</strong> <br />
香港电台《薇微语》专访 <br />
文章日期：2009年9月29日 <br />
<br />
【明报专讯】倪匡，少年向往共产党平等、均富的理想，只身由上海北上内蒙古参加解放军，但失望地发觉军中生活阶级观念森严、特权处处。自己又因在零下四十度，被迫拆掉小木桥生火取暖而面临被指控破坏交通。面对可能判刑十年之灾，他走投无路下南逃他乡，历尽有家归不得之苦后，辗转流落香港。当年他高兴地躺在维多利亚公园草地上，深深呼吸自由空气，从此乐得做一个无灾无难的自由人。转眼五十多个寒暑过去，面对中华人民共和国六十周年国庆，倪匡除庆幸自己仍是自由人外，对祖国数十年变化及经济成长依然无动于衷，不愿「回去」一看，亦没有半句建言。受了伤害的心灵，只希望能够保障个人的自由。国家大事，自觉无力挽…… <br />
<br />
倪匡的态度不一定为别人所认同，但造成他这样态度的原因，共和国不可不加以思考。 <br />
<br />
■倪――倪匡<br />
薇――《薇微语》主持李小薇 <br />
<br />
<strong>我和她水火不容 </strong><br />
<br />
薇：近期流行说「请对镜头说你是否共产党」，你是否共产党？ <br />
倪：我当然不是。 <br />
<br />
薇：你还是很讨厌共产党？ <br />
倪：对，因为共产党极权。<strong>我钟爱自由，向往个体的思想，不得不讨厌共产的思想体系，一如水火不容的道理。</strong> <br />
<br />
薇：但你当年对「她」迷恋过。 <br />
倪：1949年共产党建立政权时，全部中国人都被她迷住了。你看共产党由最初成立至建立全国政权，用了不足三十年工夫。如果得不到中国大多数人民拥护，她是不会成功的。 <br />
<br />
薇：你也是这样就信了她？ <br />
倪：当时以为中国从此有希望，但参加工作后，一看才知和想像不一样。去年中国有一本书是收集了1945年共产党的言论集，说明为什么要反对一党专政、一党专政万事不成、要怎样才有民主和言论自由。讲的全是毛主席的话和《新华日报》的社论。现在这书却成了禁书，即是说建国前共产党领导人说的话全部推翻掉。其实这些话与《零八宪章》的内容一样。 <br />
<br />
薇：你怎看到她的真面目？ <br />
倪：我到最普通的地方工作，她提倡平等，但原来阶级分明、上面的话下面的完全不能违反，与我的性格，格格不入。当年参加土地改革、斗地主，地主被判死刑，由我写判决书，我问死刑原因怎写，上面说写「地主」就可以。我说「地主」不是原因，应该说明地主犯了什么不法行为，例如强奸妇女等。上面硬说不用管，我无法接受，后来几乎要开会来斗争我。 <br />
<br />
薇：你最后妥协了？ <br />
倪：不妥协，下一回很容易是别人为我写死因报告呢！写「地主」已经不算奇怪，写「其他」的判死原因也有呢！ <br />
<br />
薇：你偷渡来港时曾吃老鼠？ <br />
倪：老鼠很好吃的，用火烧熟吃；还有吃蚂蚁蛋，一窝蚂蚁跑光后，剩下白白的蛋，我以为很不错，用手拿来吃，原来酸得很；把棉花煮熟也能顶得饿。 <br />
<br />
<strong>最坏的资本主义 </strong><br />
<br />
薇：到香港后至今，对共产党有没有改观？ <br />
倪：有些改观。<strong>共产党有改变，不过是变得更坏，现在变成最坏的资本主义。所有大企业控制在官僚手上，官僚资本主义是最无情的资本主义，不会同情老百姓的。</strong> <br />
现在最遭欺压是农民与工人，因为百分之零点七的人掌握百分之九十的资产。工人上午上班，下午回来时房子已被拆掉了，我宁愿在毛泽东时代大家都穷。让部分人富起来不是这样富法，富起来的要照顾未富的人，不可以反过来加倍压迫穷苦的人。现在新闻相片上可见煤矿老板用礼宾车送女儿上学，但路边却有衣衫破旧的小女孩……对比太强烈了，不能这样子。 <br />
<br />
薇：六十年来真的一点变化也没有？ <br />
倪：有变化但没有进步，只有愈来愈坏，所有人失去理想，只追求金钱，完全不顾道德底线，这是文革的后遗症。一些假商品完全冲破了最基本的底线，在报上看到连鱼身上都注些水增加重量。你能增加多少呢？底线全没了！ <br />
<br />
薇：有希望吗？ <br />
倪：我看不到。一个官僚贪污、一个环境污染就够，再过数十年中国连水都没得喝了。看网上照片，公共汽车上写着「吐痰请向外吐，提高个人素质」。 「向外吐」已经是提高个人素质了，这是完全没有保护环境的意识。 <br />
<br />
薇：有人说国家要先有经济发展才能讲民主。 <br />
倪：西方有这说法，但中国不同。西方理论是经济发展社会中产阶级，中产自然要求民主、自由；<strong>但中国是形成了中产阶级后，有钱的阶级却不要求政治民主，反而更加投靠极权，冀望得到更多利益。 </strong><br />
<br />
薇：你有一个哥哥也是优秀共产党员，即是说除贪官污吏外，国内也有默默工作的人，这不也是希望所在吗？ <br />
倪：当然有，如果一个都无，不可能支撑到现在。如果七千万党员有三分一是贪官，即有二千多万是贪官。 <br />
<br />
薇：还有好的。 <br />
倪：但这样是没有用的。<strong>一些好人的努力成果会被另一批人拿走，或变成巩固一党专政的（力量）。 </strong><br />
<br />
<strong>生日快乐？说不出口 </strong><br />
<br />
薇：人家六十大寿，你不可以原谅一点？ <br />
倪：<strong>六十大寿就可以原谅，那我七十大寿是否什么都可干？ ……这与年纪无关。 </strong><br />
<br />
薇：生日快乐都讲不出？ <br />
倪：<strong>她快乐时我不快乐，我希望她不快乐……我觉得她真是不快乐，而且可怜。我也很同情她，连放风筝也触动她的神经，放风筝有什么罪过呢？放上去又会怎样？其实她很坚强，但心里觉得脆弱，所以很可怜。没有人可以推翻她的，她即使全放开，让全中国人去骂，也骂不倒，政权是用枪打出来的。世界上有哪个武装力量可以推翻她的政权？她怕什么？</strong><br />
<br />
薇：香港可以做点什么吗？香港的自由可以感染内地吗？ <br />
倪：香港没有什么可做，顶多有机会就说几句话吧！但在强大的专政权力下，小百姓有什么可说呢？顶多如老子教小子：能吃一口就吃一口吧！要感染内地？一发声就会出事，例如四川地震校舍倒下，就连追问为什么这么多校舍倒下都不可以，真是太过分了。其实中国老百姓最好对付，只要不太过分都不造反，现在遍地都有人造反……伟大的毛主席说，哪里有压迫，哪里就有反抗，完全靠高压，反抗一定愈多。 <br />
<br />
<strong>今生不做中国人 </strong><br />
<br />
薇：你会回去看一看吗？ <br />
倪：我不会回去的，我没有办法，我本来说过连香港也不回来……现在已经差不多到了极限的底线了。我本来没什么不开心的，如果回去一处自己不开心的地方又何必呢？ <br />
<br />
薇：你不想家吗？ <br />
倪：我从小在上海长大，上海人很少怀念家乡的。我自己家乡也未到过。如果我喜欢怀念，我在任何地方都会怀念香港。我在香港生活得最久了。有本书叫做《来生不做中国人》，是天下第一好书，但不够彻底。我说今生不做中国人，彻底失望。 <br />
<br />
薇：但你好多朋友都回去看看，查良O、S、陶堋… <br />
倪：是的，他们喜欢嘛。我始终觉得，所有不同想法都可以并存，所以我最讨厌与人辩论，最不喜欢统一意见了。不统一意见的人都可以做朋友。如果意见不同就不可以做朋友，那我自己都变成共产党了。 <br />
<br />
薇：你一生最执着最坚持什么呢？ <br />
倪：<strong>我坚持要有个人自由，人应该生活在一个有个体自由的社会中，</strong>身体不受伤害。我很幸运，在香港数十年，我的自由完全不受干扰，而且人人说我为所欲为。我当初来香港时，躺在维多利亚公园草地，完全没有人干涉，自出自入。 <br />
<br />
薇：你真是一个很开心的人。 <br />
倪：中国数千年历史，严格来说这三十年已经是中国盛世，死人算少又无战争。 <br />
<br />
薇：这即是有进步了吧！ <br />
倪：但这是没有用的，极权统治一日未改，一日靠上边在位人的态度而定，上边的人今日如此（当然好），但一念之间可以改变国家命运嘛！ <br />
