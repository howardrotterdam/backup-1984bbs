倪匡（原名倪聪，字亦明，笔名<strong>卫斯理</strong>、沙翁、魏力，1935年5月30日－），中国浙江宁波市镇海县人，生于上海市。1957年移居香港，1992年移居美国旧金山。他是华人科幻小说界里的知名作家，著名作品包括《卫斯理系列》 、《原振侠系列》、《浪子高达系列》、《神探高斯系列》、《亚洲之鹰罗开系列》、《非人协会系列》、《女黑侠木兰花系列》、《年轻人与公主系列》、《神仙手高飞系列》、《侠盗影子系列》等。倪匡写作范围甚广，亦曾写作怪异小说、色情小说，也曾创作少量武侠小说。倪匡亦曾帮助朋友金庸撰写在报刊上连载的武侠小说《天龙八部》中的部分章节，倪匡不喜欢书中的角色阿紫[1]，故在写作中把她弄瞎，让金庸花了好些时间去令阿紫的双眼复原。<br />
<br />
倪匡是金庸的好友，曾希望金庸写色情小说，金庸只一笑置之。[来源请求]此外，倪匡也创作了超过三百多个电影剧本。另外亦曾在1989年至1990年间，与黄和蔡澜合作主持亚洲电视清谈节目《今夜不设防》。倪匡在《am730》的专栏内多次提到他在年轻时很喜欢看AV，家中就收藏了一大堆[2]。<br />
<br />
倪匡先后使用过的笔名包括：卫斯理、沙翁、岳川、魏力、衣其、洪新、危龙等等。<br />
<br />
另外，民间相传的霍元甲首徒陈真亦其实是倪匡在电影《精武门》里虚构的人物。电影中多幕经典的场面，如日本公使抬着“东亚病夫”的牌匾赠与陈真，亦是倪匡杜撰。这些都由他在《am730》的专栏及电台节目中陆续说出来。<br />
<br />
倪匡的妹妹亦舒是文艺小说作家。倪匡的儿子倪震曾任电台主持人，而香港1990年代红星周慧敏则是倪震的妻子(2008年12月18日宣布申请注册)。倪匡另有一女儿倪穗。<br />
