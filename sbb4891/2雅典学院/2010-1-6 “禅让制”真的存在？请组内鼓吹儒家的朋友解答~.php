																											<div id="postmessage_417805" class="t_msgfont">尧舜禹“禅让”之疑？ <br />
 <br />
徐志高 <br />
 <br />
 <br />
<br />
永州市教育局 徐志高<br />
<br />
《尚书》有《尧典》、《大禹谟》等篇，叙述尧、舜、禹禅让的故事，千古流传，为后世津津乐道。对这个故事，我始终取怀疑的态度。疑点主要集中在尧舜禹之间的血缘关系上。<br />
<br />
据司马迁《史记》之《五帝本纪》和《夏本纪》载，尧舜禹的共同祖先是黄帝。黄帝娶正妃嫘祖，生下两个嫡子，一曰玄嚣，一曰昌意。<br />
<br />
玄嚣生子 极， 极生子高辛。高辛即是帝喾。帝喾与其四妃生子挚，与其三妃生子放勋。放勋即是尧。尧是黄帝的第五世孙。<br />
<br />
昌意生子高阳。高阳即是帝颛顼。颛顼生子鲧和穷蝉。鲧生子文命。文命即是禹。禹也是黄帝的第五世孙。穷蝉生子敬康，敬康生子句望，句望生子桥牛，桥牛生子鼓叟，鼓叟生子重华和象。重华即是舜。舜应是黄帝的第九世孙，也是禹的第五世侄孙。<br />
<br />
由此可知，尧和禹本是一对远房叔伯兄弟，并同是舜的高祖辈。从辈份上来看，尧让位于舜还免强说过得去；而舜让位于禹，实在有些难以理解了。<br />
<br />
在《五帝本纪》里，司马迁这样写道：“尧立七十年得舜，二十年老，令舜摄行天子之政，荐之于天，尧辟位，凡二十八年而崩。”“舜年二十以孝闻，年三十尧举之，年五十摄行天子事。年五十八尧崩，年六十一代尧践帝位。践帝位三十九年，南巡狩，崩于苍梧之野。”唐朝的韩愈在《论佛骨表》一文中认为，“帝尧在位九十八年，年一百一十八岁。”尧崩之年，舜年五十八岁。显然，尧比舜年长六十岁。由尧和禹是兄弟辈推知，禹比舜年长六十岁左右，应是无可置疑的。这样就引出了两个怀疑：其一，当玄孙辈的舜已近花甲之年时，高祖辈的尧还能健在，是哪家子的长生不老药让他如此高寿并堪当帝任？其二，更为奇怪的是，当舜一百岁时，居然还把帝位让给比自己年长六十来岁的高祖辈禹，而禹居然也在位几十年！<br />
<br />
历史上，对尧舜禹“禅让”之说，一直争论不休。最早提出疑问的是荀子。他在《正论》中说：“夫曰尧舜禅让，是虚言也，是浅者之传，是陋者之说也。”孟子对此也是持疑的。万章曾问孟子：“尧把天下给了舜，有这回事吗？”孟子说：“没有。天子不能把天下给人。”万章对这个回答并不满意，再追问：“可是舜确实有了天下呀，那是谁给的呢？”孟子只好说：“天给的。”当然，提出“民贵君轻”理念的孟子，是想通过迂回的方法，神不知鬼不觉地把万章引导到“君权天授”的路上去。但他的确不敢肯定，有尧舜禅让之事也意在言外。说得更露骨的当属韩非子了。他在《说疑》一文中说：“舜逼尧，禹逼舜，汤放桀，武王伐纣，此四人者，人臣弑其君者也。”韩非子鉴于战国时的社会乱象，鼓吹厉行法治，好发一些偏激的议论，有哗众取宠之嫌也是事实。但也不能由此认为他的观点就一定虚妄。实际上，他的“逼退”说，来源于与他同时代的《竹书纪年》。在这部现已失传的历史著作里，作者认为：“尧德衰，为舜所囚。舜囚尧，复偃塞丹朱，使父子不得相见也。”后世的刘知己、司马贞等学者，也持同样的观点。台湾著名作家历史学家柏杨先生，在他的《帝王之死》一书中，论定尧被囚禁而死，舜死于蛮荒。两人都不得善终。柏杨先生擅长把历史当作文学来叙述。<br />
<br />
不管公说婆说，司马迁对于尧舜禹三位圣贤血缘关系的记载，总是一道绕不过的坎。现代历史学家范文澜也承认：“据传说，尧是帝喾的儿子，距黄帝五世。舜是颛顼的七世孙，距黄帝九世。禹是颛顼的孙子，距黄帝五世。三人世次，显然有很大的讹误。”（范文澜著《中国通史》第一册）。先入为主地认定存在尧舜禹禅让之事，再去读历史，当然以为历史记载有“讹误”。如果相信历史记载无误，再来观禅让一事，是否能发现其中的“荒诞”呢？<br />
<br />
历史上是否有过“禅让”之事？我认为，社会关系要适应当时的生产力发展水平。在与自然斗争和部落间争斗得不可开交时，是需要能人领导的。所以，禅让是可能也是必要的。但要说禅让一定发生在尧舜禹之间，并不足信。<br />
