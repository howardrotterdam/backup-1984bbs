																											<div id="postmessage_417813" class="t_msgfont">古代真有“禅让”吗？<br />
作者： 李兴濂<br />
<br />
　　“禅让”最早出现在《尚书&#8226;尧典》等篇，叙述尧、舜、禹“禅让”帝位的故事。据说，尧在帝位，向四岳咨询继承人选，四岳推举舜。尧于是“以二女妻舜，以观其内；使九男与处，以观其外。”经过种种试验，让舜摄位行政。二十八年后尧死，“百姓如丧考妣”，舜正式即位。舜后来也照样咨询众人，选出禹来摄行政事。禹在位时，众人推举皋陶。皋陶死，又选皋陶子伯益为继承人。这就是“禅让”。但当禹去世的时候，禹子启夺伯益位自立，启开刽了“家天下”的局面，“禅让”制度也就从此被父死子继、兄终弟及的宗法制度所代替。<br />
<br />
　　尧舜禹三代“禅让”的真实性如何？孔子说“文献不足”，司马迁也认为“五帝尚矣”，“荐绅先生难言之”。“舜囚尧，复偃塞丹朱v丹朱，尧之子w，使不与父相见也。”v《史记&#8226;五帝本纪》“正义”引《括地志》转引《竹书》w《辞海》记有一说尧到了晚年，德衰，为舜所囚，其位也为舜所夺。之后舜为禹所放逐，死在南方的苍梧。到了春秋战国之际，“禅让”这一渺茫的历史陈迹经过加工制作，涂上了理想化的油彩，尧舜禹三代成为“天下为公，选贤与能，讲信求睦”的大同世界，代表了社会政治的最高境界，从诸侯纷争的乱世中回顾往昔，那真是“失去的乐园”了。从此以后，“禅让”一词反复出现，而且从虚无缥缈的传说进入现实政治领域，变幻出光怪陆离的景象。<br />
<br />
　　战国末年燕国搞了一次“禅让”的试验。燕王哙在说客的怂恿下，想得到让贤的美名，居然把王位让给宠臣子之。之子杀了反对他的燕太子，国内大乱，齐国乘机伐燕，哙和子之都被杀，燕国几乎覆灭。到了汉代，汉昭帝元凤三年v公元前78年w，各地奇迹迭出，泰山有巨石直立，数千白鸟飞集其下；皇家上林苑枯死倒地柳树起立复生等等。大臣推论《春秋》经义，上书建言“求贤人禅帝位，以顺天命。”结果以“妄设妖言惑众，大逆不道”之罪伏诛v《汉书》本传w。宣帝神爵二年v公元前60年w，又有大臣上书官以传贤，结果下狱自刭。汉家天子是决不能“禅让”的。到了王莽时，演了一出“禅让”的闹剧，刘姓汉朝改成了王姓新朝。传说中的“禅让”终于变幻成为一种政治手法，改朝换代的斗争以“禅让”为美丽的装饰，攘夺其实。赵翼《廿二史札记》卷七有《禅代》一题，叙述了魏、晋、南北朝、隋、唐、五代连续不断“假禅让为攘夺”的史迹，帝王们在杀伐篡夺之余，都效仿了“受禅”的典礼，真可谓衣钵相传。<br />
<br />
　　这套衣钵最后传到了二十世纪的袁世凯手里。1915年底，袁世凯搬演了帝制丑剧，北京故宫里备齐了绣金龙袍，珍珠冕旒，登基登天的大典筹办就绪。但是，四面楚歌惊破了袁世凯“受禅”的迷梦，他撤销承认帝位之案，历史不再容许重复这一类讽刺了。<br />
<br />
　　“禅让”制度成了人们美好的神话，追求的目标。就算是古代社会曾有过，但它也不是那么美好的。尧把帝位让给了舜，并不是由老百姓来认定，还是由尧一人说了算，想给谁就给谁。“你办事，我放心”，至于老百姓放不放心，他就不管了。“禅让”制度说到底是权力私授制，是完全由老掌权者指定权力继承人的制度。什么时候由全体人民选举领导人，那才是人们向往的制度。</div>

							
							
							
							
