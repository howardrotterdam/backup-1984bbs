　　 <br />
　　本书中，柏杨先生最常使用的一个词组为“政治挂帅”。在政治正确的大旗下，他认为孔孟篡改了某些史实。比如，对尧舜过分地吹捧，夸大他们的智慧和美德；尧舜所谓的“禅让”，其实舜将反对其的四个大臣（儒家所谓的“四凶”）屠杀之后，尧的无奈之举；到最后，尧死在舜安排他进行的所谓“全国巡回视察”的旅途中，这是中国第一位死于谋杀的帝王，且“沉冤千古”；而大禹治水、三过家门而不入的背景，也出乎意料，书中指出，禹的父亲（所谓“四凶”之一）正是死于舜的屠杀，按理说作为家属的禹也应被放逐，但由于禹作为当时治水“高级技术人员”，舜不得不用他，治水期间也是禹培植势力的时候，其不入家门，是“不敢入”而非“不肯入”，最终禹势力养成，仿效舜对付尧的手段，将舜逮捕充军，死于蛮荒。 <br />
　　 <br />
　　这绝不是我以前读到的“历史”，而是被包装过的“糖衣炮弹”，没有美丽的“禅让”，却充斥着赤裸裸的政治图谋、血腥的谋杀。柏杨先生认为，这是孔子看到他所在时代的乱糟糟，于是“托古改制”，而尧舜则被“隆重选中”，“一条又僵又冷的死虫，经过孔丘先生吹口仙气，立刻变成了花蝴蝶”。而这口“仙气”的魔力却如此之大，一直影响到几千年后的今天。 <br />
