													<h2>大连尸体加工厂――冯哈根斯生物塑化有限公司</h2>
						
						
																											<div id="postmessage_538836" class="t_msgfont">位于大连高新技术产业园内的冯・哈根斯生物塑化(大连)有限公司，一个专门从事尸体加工的公司，它的存在引起了这份杂志的质疑。加工尸体，一个有悖于传统伦理的行业，另类地存在于大连这个颇具知名度的城市，它到底能给这个城市带来什么？ <br />
为此，本报记者前往大连采访了“哈根斯”。 <br />
目击尸体加工 <br />
“生命终结，但尸体却可永久保留。”听上去，这似乎有些不可思议，然而今天，在世界上，在中国，在大连，这却变成了现实。这项技术，就是高新技术―――“生物塑化”技术。 <br />
冯・哈根斯生物塑化(大连)有限公司(以下简称冯・哈根斯公司)是一个专门从事生物塑化工作的公司，他们把尸体解剖、脱水、定型之后制成人体标本，进行永久性保存。想像中，这一系列的操作过程令人陌生而畏惧！ <br />
近日，记者来到了位于大连高新技术产业园内的冯・哈根斯公司。该公司依山傍海，位于产业园的西北侧。7层的办公楼从远处看上去，特别雄伟壮观。与其相隔10余米的2层楼房就是人体制作车间。 <br />
<br />
储藏 <br />
在该公司行政部芦淑华部长等人的陪同下，记者首先来到了标本储藏车间。一个个2米多长、1米多宽、1米多高的白色金属箱子，摆满了整个屋子。这里没有嘈杂声，没有凌乱的东西，室内温度适宜，通风效果也不错，一切显得十分整洁。 <br />
因为是第一次，记者未进尸体加工厂之前，就有一种毛骨悚然的感觉。如今，装着一具具尸体的金属箱就在眼前，反倒有些坦然和镇定。 <br />
“能否打开，看一看？”记者问道。 <br />
这时，两名身穿塑胶工作服的男子走过来，把金属盖打开。呈现在眼前的是，箱子内盛满了灰黑色的液体。里面飘浮着3块用白布包裹的东西。 <br />
“这就是尸体！”一位工作人员说。 <br />
白色的包裹看不出人形。工作人员说，这些是原始的标本，首先要经过浓度为20%的福尔马林灌注，然后在福尔马林的真空包装里放置至少4个月的时间，之后才可以解剖或运输。福尔马林在此起到的作用是固定、杀菌。 <br />
“这里的尸体都是德国志愿者捐赠、经过国家质量监督检验检疫总局和辽宁检验检疫局的严格检查后从国外进口的！”一位陪同人员强调说。<br />
<br />
解剖 <br />
走出地下室，记者又来到了解剖室。这是一个朝南的房间，室内明亮，工作台上面，整齐摆放着尸体，尸体上盖着白布。 <br />
再看，眼前的一幕，就有些吓人了：150多平方米的车间里，20个解剖台一字排开。从男到女，从幼到老的遗体，正在被一群年轻人解剖和肢解。 <br />
每个解剖台旁边有三四名年轻的工作人员，娴熟地使用解剖镊和解剖钳等工具，在解剖过程中，每个人都表情严肃，没有语言，只有动作。“镊子……钳子……托盘……”他们在重复着这一系列的动作，认真地将尸体肌肉组织当中容易腐烂的脂肪物等一一剔除，暴露出来的是神经系统、肌肉，还有骨骼。“这么细心地清理一具人体标本，平均需要1500―2000个小时，相当费力！”一位正在实施解剖的女士说。 <br />
对工作人员的表现，公司老板哈根斯十分满意。他说：“我们的解剖工作人员几乎都是毕业于中国各医科大学临床医学系的，解剖技术是一流的！” <br />
记者凑到没有人皮的尸体前，闻了闻，并没有发觉任何异味。<br />
 <br />
脱水 <br />
解剖工作完成后，就是进行“脱水工作”。 <br />
该公司的脱水车间并不是很大，屋内有6个硕大的盖着盖儿的金属箱子。“解剖之后的尸体就泡在这些箱子里。” <br />
该车间的工作人员讲，他们的工作，就是把清理好的尸体进行脱水。在这之前，尸体已经被浸染了福尔马林，他们的工作就是要在低温的丙酮浸液中，用技术手段在冰冻的体液中把福尔马林排除，用丙酮进行置换。 <br />
<br />
切片定型 <br />
一切的工作结束以后，在冷冻状态下，可以用锯把尸体切成3.5mm厚的切片。人们可以通过这些切片区别受损与正常的器官，如吸烟的肺与正常肺的区别；脂肪肝与正常肝脏的区别等。 <br />
尸体，除了冷冻做成切片外，就是定型了，做成各式各样的造型。走进定型车间的时候，眼前豁然开朗，仿佛走进了一个艺术品加工车间。只见在一个非常宽敞的房间内，那些已经被处理好的尸体，被塑造成各种姿势，有的坐在椅子上，单手托着下巴做思考状；有的正在奔跑中，伸出一只脚做踢球状，显得生龙活虎；还有的双脚腾空而起，仿佛勇士腾空而起……（华商晨报）</div>

							
							
							
							
