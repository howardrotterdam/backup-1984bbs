																											<div id="postmessage_538846" class="t_msgfont">巴黎法庭判决：禁止中国人尸体展 <br />
<br />
来自：鸟博 <a href="http://www.oizoblog.com" target="_blank">www.oizoblog.com</a><br />
<br />
翻译：碧水<br />
<br />
原载：法国新观察报<br />
<br />
巴黎终审法庭紧急审理法官本周二作出裁决，终止目前正在巴黎展出的人体解剖展《窥视人体》，指出“法律规定人类遗体最终的归宿是墓地”。<br />
<br />
路易－马利&#8226;兰雅尔&#8226;德拉布莱切尔法官限展览主办方于24小时之内关闭此展馆，并要求他们请法庭执达员作出一份遗体参展名单。另外，需将这些遗体妥善保管，以便“由法国当局主官机构寻找一条符合相关埋葬法规的办法”。法官规定，如有延误，主办方需每日付出两万欧元的罚金。<br />
<br />
此次展览的组织者尚万公司（Encore Events）可以在当日提起上诉，但上诉没有得出结果之前，展览必须在法定期限内关闭。<br />
<br />
兰雅尔法官在裁决书中指出：“人的遗体及肢体应予以土葬或火葬，或者由公法法人机构进行科学收集”，而“私人占有”是“违法的”。<br />
<br />
他进一步强调，尽管主办方是以向公众作科学解说为目的，但法律“只允许确定的医学用途”，“禁止一切将人体商业化的行为”。他还说：“尚万公司以此寻求商业利益是极为不妥的作法。”<br />
<br />
同时，法官对道德层面提出质疑，认为此展览损害了人体应有的尊严，而将尸体切割展出有失庄重。他说：“人体展中详尽展示了各种艺术动作，却声称以此来代替表现个体，个体的自我表现。”<br />
<br />
“至此，尽管普遍认为达到了唯美的境界，但展出遗体和切割的人体器官在科学上是不合法的，更不必说随意在器官上涂色，展现各种非现实感的动作。”兰雅尔法官的结论是：“在这一点上显然有失稳重。”<br />
<br />
此次展览的科技专员埃尔维&#8226;洛朗在马德莱娜空间十二接受采访时说，这种“争论毫无意义”，因为展览本身“在技术层面上无懈可击”，“也对人体给予了足够的尊重。我们当然要尊重人体，展览的目的就是让人们更好地了解自己的身体。”晚间17点将有另一次采访。<br />
<br />
此案源于两个人权组织“共同中国”和“反对死刑联盟”提交的诉讼，紧急审理法官据此进行审理。该组织认为此一展览有损人类的尊严、体面和庄重，并破坏了人体的不可侵犯和整体性。他们甚至怀疑此展非法使用中国人的遗体。<br />
这些中国男女遗体在巴黎的展览始于2月12日，有些是切割的器官，有些则摆出了日常生活的姿势。展览设于巴黎玛德莱娜空间十二，原定于5月10日结束，之后于5月22至8月23日在万森讷的百花公园内展出。</div>

							
							
							
							
