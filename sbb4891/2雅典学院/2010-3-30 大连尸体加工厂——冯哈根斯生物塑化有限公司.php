　　晨报讯（记者朱烁）一根根鲜明的红色血管，条理清晰的肌肉组织蕴涵着力量，圆瞪的眼睛里瞳孔依稀可辨，仔细看去关节处还有残留的黏液……日前，“塑化人体标本展”悄然亮相北京自然博物馆。 <br />
<br />
　　本次展览包括6件完整人体标本和近200件器官标本。上至不同剖面的颅骨，下至足部每一根趾骨，心脏、脾、肺的外观形态，人的消化系统、神经系统等一一在观众面前呈现。与以往的人体器官展不同，这些展品采用了塑化技术和管腔铸型技术，将尸体的水分和有机物置换成树脂，尽可能地保持器官的原样。 <br />
<br />
　　据了解，此次展览的人体标本是由大连医学院提供的。本报记者曾赶赴大连，对“尸体工厂”进行追踪。相关专家对“塑化人体标本”作出解释，首先用福尔马林液将尸体进行固定，然后经过解剖和锯切，再用酒精、丙酮对尸体进行脱水处理，把组织中的水分置换出来。接下来把一种我们俗称为硅胶的固定剂装入经过丙酮处理过的尸体里，用这种胶再把尸体里的丙酮置换出来。在这个过程中，组织的间隙会完全被胶状物充斥，凝固后的尸体也就因此变成“胶人”了。经过这种科技手段处理后的人体标本质感很好，标本保存的时间几乎是永久性的。由于制作人体标本的胶状物质的科技含量高，所以国外对这种胶的原料配方是保密的，国外用这种胶做一个标本的成本至少要上万元。 <br />
<br />
　　但对这种尸体展览应如何看待，我国著名生命伦理学专家邱宗仁先生认为，即便是尸体，因为有过生命，因此有尊严，应该被尊重。对于尸体来说，任何商业性的活动都是对生命的不尊重。著名医学伦理学专家陈天民也认为，尸体标本售票展出有悖公德。陈教授说，虽然在利益驱动下什么事情都可能发生，但他依然认为遗体（标本）展览在中国是行不通的。因为从伦理上讲，这种不人道的做法中国人不能接受。陈教授认为，尸体或人体的标本只应该应用到教学和科研上，而不能如此公然地售票展出，对尸体的任何商业化做法都是违背伦理道德的，是行不通的。</div>

							
							
							
							
