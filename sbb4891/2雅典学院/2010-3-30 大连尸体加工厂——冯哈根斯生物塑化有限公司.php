巴黎尸体展： 法官的裁决<br />
<br />
 <br />
 <br />
--------两个人权组织在法国巴黎以“违反基本人权”的理由对中国尸体展“我们的身体，打开的身体”提起诉讼， 要求将其终止。<br />
 <br />
<br />
一个由17具来自中国尸体组成的生理知识展览即将在巴黎拉马德莱纳空间（l’espace La Madeleine ）举行并将持续到5月10日。 该展览取名为：&quot;&nbsp;&nbsp;我们的身体， 打开的身体”。<br />
 本周两个人权组织“共同中国”和“反对死刑联盟”向法院提起终止展览的诉讼。 “展览是否违背了基本人权”成了人们讨论的问题，也将等待司法裁定。。<br />
 <br />
 <br />
在本周二紧急庭审上，原告组织的律师 RICHARD SEDILLOT提出：法国的现行法律明确规定了对人类遗体的处理必须坚持&quot;&nbsp;&nbsp;尊重，体面，端庄”的原则。 并且对人身体的尊重不能因为死亡而中止。 另外，该律师也表示：这些用于展览的青年尸体有可能曾是囚犯或者是死刑犯的遗体。把它们剥皮切开，展示其内脏， 并造型成正在下棋，踢球等样子。 是耸人听闻的！并非所谓科学。<br />
 <br />
该展览在售票15欧元，说明了其本身是一场商业展览并且已经吸引了20万法国观众，而在其网站上，标明了其是“艺术性并富有教育意义的”。<br />
 <br />
展览的主办方， ENCORE EVENTS 公司经理PASCAL BERNARDIN的律师TOMASI向其顾客强调了展览的目的在于科普，消除人们身体的神秘性。-<br />
 <br />
同样，PASCAL BERNARDIN 表示， 这场展览也将对法国的器官捐献事业的发展起到积极作用。<br />
