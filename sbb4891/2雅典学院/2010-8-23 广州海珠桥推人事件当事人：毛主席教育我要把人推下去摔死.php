													<h2>广州海珠桥推人事件当事人：毛主席教育我要把人推下去摔死</h2>
						
						
																											<div id="postmessage_793317" class="t_msgfont">赖健生蓄起了70厘米长的白胡须。他以此来表达受委屈的心情。“我真正为人民牺牲，我应该得到的，(是)见义勇为的小小名誉”。他说，一年前的海珠桥事件让他夜夜失眠，恶梦连连。 <br />
<br />
&nbsp; &nbsp; 一年零三个月前，67岁的赖健生把上桥讨工程款、导致交通阻塞5小时的陈富超推下海珠桥，一度惹人争议。 <br />
<br />
&nbsp; &nbsp; 8月21日，赖健生参加广州市城管委召开问计市民商贩座谈会，他再一次回到公众视野。 <br />
<br />
&nbsp; &nbsp; 远在化州农村的陈富超说，海珠桥事件让他腰部受损，右手“粉碎性骨折和九级伤残”。今年2月，他着手准备诉讼材料，把赖健生告上法庭。 <br />
<br />
&nbsp; &nbsp; 今年3月，赖健生接到海珠区人民法院发来应诉通知书，陈富超向他索赔28.5万。 <br />
<br />
&nbsp; &nbsp; 陈富超说，今年8月他才把右手的螺丝取出。而赖健生则坚称自己是功臣，“如果重来我还是会推”。他说，他要“到中央去申报见义勇为”。 <br />
<br />
<br />
&nbsp; &nbsp; 对话赖健生 <br />
<br />
&nbsp; &nbsp; 赖健生（以下简称“赖”）：我一直病，一直吃药。主要是治疗心理、失眠、抑郁的，神经衰弱，心理治疗。现在双脚都冷、痹，手冻。所以说，冤屈是会死人的。我发恶梦老是被人追，都是很不顺心的。 <br />
<br />
&nbsp; &nbsp; 南都：到底是谁冤枉了你？是群众，是媒体，还是陈富超？ <br />
<br />
&nbsp; &nbsp; 赖：警察说，“你以为你是超人啊。”可报纸上说，(当时)2.5万部车受阻，有5万人，这么多人没人上去。 <br />
<br />
&nbsp; &nbsp; 我跑上桥解决了三个问题：一是将他救下。二是帮助警察解困。三是终止犯罪分子对人民群众利益的损害。 <br />
<br />
&nbsp; &nbsp; 警察说：“如果他没受伤，你就没事”。他受伤不是我的责任喔。推他下地就是我的责任。我推他下救生垫里。因为救生垫充不满气，以前从来都没有人掉下来的。 <br />
<br />
&nbsp; &nbsp; 南都：你觉得责任不在你？ <br />
<br />
&nbsp; &nbsp; 赖：责任不在我，为什么要我上去呢？两台警车，两台消防车，(警察)都没想其他办法，就是和他谈判，好像饮茶倾计那样谈判。对于无赖有什么谈的？耗下去最损害的是人民群众。 <br />
<br />
&nbsp; &nbsp; “如果重来我还是会推” <br />
<br />
&nbsp; &nbsp; 南都：你当时乘车途经海珠桥回家，遇到跳桥事件就下车了？ <br />
<br />
&nbsp; &nbsp; 赖：对。我当时就去和(桥)下面的警察谈，我说，“你们的工作方法太过不作为”。为什么5个小时还没解决呢？人民受害我的心就痛。 <br />
<br />
&nbsp; &nbsp; 南都：为什么这么说呢？<br />
<br />
&nbsp; &nbsp; 赖：因为我学毛泽东思想。红色的小红簿(语录)我都带在身上。当时我觉得，你损害了人民群众，我请求警察让我上去。 <br />
<br />
&nbsp; &nbsp; 南都：但你推陈富超下去后，他生死未卜，那一刻你怎么想？ <br />
<br />
&nbsp; &nbsp; 赖：就是为了和犯罪分子斗争。 <br />
<br />
&nbsp; &nbsp; 南都：为什么你一直认为陈富超是犯罪分子？<br />
<br />
&nbsp; &nbsp; 赖健生：如果按毛泽东时代，这叫做破坏社会主义的反革命分子。我最终还是叫他犯罪分子，因为他涉嫌扰乱公共秩序，已经造成这些事实，已经是个罪犯。 <br />
<br />
&nbsp; &nbsp; 南都：如果重来还是会这么做？ <br />
<br />
&nbsp; &nbsp; 赖：会，还是会推。人民受损害我的心就流血。但现在会想得更细，更智慧一点，不要这么冲动。会谈15分钟咯。我当时就是急躁了一步，推了他下去。 <br />
<br />
&nbsp; &nbsp; 南都：这件事过去了一年，你有没有后悔？ <br />
<br />
&nbsp; &nbsp; 赖：我认为我的行动是无愧于人民。我不后悔。 <br />
<br />
&nbsp; &nbsp; 南都：但他的受伤很可能是你的行为导致的？ <br />
<br />
&nbsp; &nbsp; 赖：不是我的原因造成。因为在我判断里救生垫是绝对安全我才这样做的。所以这个气垫伤人的责任不是因为我推他下去。 <br />
