Ps:已远离这个行业，我想应该与职业道德无关。贴出来资料中的观点仅代表man-eleven扮演的man-eleven的立场，与man-eleven无关。小组现在30分钟后就不可以编辑了，操<br />
============================================================================================================================================================================================================================================================================================================分割线==================================================================<br />
先说个笑话：朱F基当总理的时候把卫生部长张文康和药监局局长郑筱萸叫到一起，表情十分严肃凝重地（纯猜测）对张文康说：医出了问题我拿你是问；然后又对郑筱萸说：药出了问题我拿你是问。结果两个家伙都出事鸟……<br />
<br />
一直在谈医药改革，改来改去还是那样子，归根结底，就是你们天天讨论的问题。国内主要是以药养医的状态。<br />
<br />
好了，不谈政治。<br />
<br />
药品从出厂到病人手中的（医院这块）过程如下：国家规定药品的流通必须经过医药公司，然后医院再到医药公司进药，药这样就到医院的仓库了，根据“需要”再拿到医院的药房。医生开完处方，你交完钱就可以去取药了。<br />
<br />
<br />
谈药品一定离不开医药代表（负责药品的销售工作），医药代表主要分为otc（药店），医院，商务（和医药公司打交道，负责药品从厂里调拨到医药公司，渠道，调货等问题）三块。药企都会重点抓医院这块的销量，毕竟还是通过医院产生的销量多，利润大。药企也会耍小聪明把同样的药制成不同的规格，比如药店的36粒，医院销售的是24粒，为了就是保住不让医院这块的销量下滑。<br />
<br />
因为同种类的药品有很多，所以医院进药不是随便谁家的药都会进的。那么药是怎么进的呢？医院一般化会有个副院长分管这一块的工作，下面还有药剂科主任，不同的医院说话算数的人不一样，时而副院长，时而是药剂科主任（占大都数），这取决于他们之间的信任程度。医院一般化都有叫药事委员会的有关部门，假如一种药要想进到医院，必须经该科室的主任提交报告到药剂科，药剂科批准以后拿到药事委员会（委员一般化由各科室主任或者所谓的“刘一刀”组成，难说，说不定主任那天有事不在，派个信得过的手下过去）讨论，通过，这种药就可以进医院了。这几个过程中就需要药代去公关，打通每个关节。即使一个眼神的差错，可能就会导致这种药进不去，而药事委员会开会的时间间隔很长，少则几个月，多则几年，所以作为一个医药代表，他们的压力就会很大，药连医院都进不去，何谈销量业绩。<br />
医药代表所属的公司都会给药代们公关的费用，比如说开发（就是把药弄进医院的意思）一个三级的医院给5000块钱的费用，二级医院的费用是3000块钱，一级医院乡镇卫生院多少钱等等，每个公司政策是不同的。<br />
<br />
外企可能做学术方面多点，就是让医生旅游啊，大型医药学会议的参加机会，镀金啊，提高医生的知名度，也会给钱。<br />
<br />
药进到医院以后，医药代表为了提高销量，这时候就会做医生的工作了，医生是他们的枪手嘛，他们互相利用。一般化月末或者月初的时候，药代开始统计每个医生的销量，按一盒/瓶……（以下统一用盒）多少钱把钱偷偷的给医生。所以医生开该药开的越多，这些灰色收入就越多。一盒给多少钱一般化是该药品最高零售价的至少20%。药代开始找他们做工作的时候医生一般化都会问问最高零售价是多少啊，意思你能给我多少钱，达到这个数以上，感觉你还是个不错的人的话就会“干了”。药代是怎么统计到每个医生用多少盒？药房或者医院的机房有专门的这样人才，这个人一般化会收取药代0.5-1元/盒不等的钱，比如他统计出这个月有三个医生用了这种药，A用了100盒，B用了200盒，C用了300盒。那么总数是600盒，这位人才得到的钱就是600×（0.5-1），然后药代按照这个方子，把钱分到abc每个医生，统方对于一个医药代表非常的关键。否则引发医生之间的冲突就会影响整体销量，比如说A医生说妈的我这个月明明开了102盒，怎么才给我100盒的钱？呵呵，有时候就是这样，国内很少还有医生有职业道德了。<br />
<br />
医生为什么会开这种药给你呢？一般话而言，该药的药代给他的钱多和他关系好他信任该药代啊，所以他才不会在乎药品的质量和效果呢，只要这种药品不会出人命，不出事，不影响他的仕途，不让他们上新闻。基本上每个医生都是别人的客户，他开处方是有目的的。有时候会感觉实在不好意思良心发现下，会稍微平衡下用下同类药品几盒，如果医院有同类药品的话。所以同类药品之间竞争十分的激烈与残酷。有的医院会采取限方措施，就是一个处方不能开超过多少钱的药，防止一种药开的过多，造成其他厂家的不满，他们集体举报就麻烦了。<br />
<br />
说的比较多的是钱方面的，这是必须的（有个别医生会拒绝你给他钱，好医生还是有的），公关的过程有很多手段，性贿赂（特指女医药代表）了，请客吃饭了等等绝对少不了。<br />
<br />
医药就是这么个情况。<br />
<br />
<br />
再说说药店的事，张书记说的那个医托可能是药店的职员，因为啊，药店的职员和医生一样的货色，经其推荐出去的药品销量越多，otc代表给他们的回扣也就越多啊。以此类推。。。关于不同的药店同样的药但是价格不一样的问题，一个是品牌不一样造成的，另一个可能是otc的代表一个工作就是尽量保持住各个药店的价格一样，要不药店之间形成“恶性”竞争，造成零售价下降，属于工作的失误，价格不一样的很大原因可能是该药的医药代表工作没做好。<br />
<br />
本人不希望治病的时候选用中药，简单高度概括下：成分不明确。中药中的确会有有效成分可以治疗你的病，但是那些未知的成分谁给你保证啊。所以啊，王老吉别喝啊，他有中药成分。喝了以后的确会有降火功能（我喝完的感觉）。另三联的土摩托老师的关于中药的观点，我表示严重同意。<br />
