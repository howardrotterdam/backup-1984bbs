参与者没有重复答题，多次提交结果；<br />
参与者真实的回答了问题；<br />
其间程序没有任何故障；<br />
测验相对准确的反映了参与者的倾向。<br />
<br />
《政治指南针》共有50个问题，每个问题3个选择。总分范围-126到+126。正值代表右倾，负值代表左倾。截止到2006年10月1日，共得到了超过5千个（5,000）有效测验结果。（更新：截至2008年5月31日，超过27,000份有效测验结果，统计<br />
