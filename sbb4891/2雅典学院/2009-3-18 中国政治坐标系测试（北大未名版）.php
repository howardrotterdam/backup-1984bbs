<br />
政治观念坐标，负值为左(Authoritarianism)，正值为右(Libertarianism)。<br />
<br />
经济观念坐标，负值为左(Communism, Collectivism)，正值为右(Neoliberalism)。<br />
<br />
社会文化观念坐标，负值为保守(Conservatism)，正值为自由(Liberalism)。<br />
