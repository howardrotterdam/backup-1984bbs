&nbsp; &nbsp; &#8226;王维林（？）<br />
&nbsp; &nbsp; 王维林（？）。1989年6月5日，北京戒严部队天津警备区坦克一师的十余辆坦克从天安门广场离开时，有一个人挡在坦克前阻止坦克离开。这个人据说叫王维林。美国公共广播协会公布的一段录像中显示：在东长安街上，王维林跑向一列正在前进的坦克，挥手示意坦克后退。坦克停止了前进，并试图绕开他，但他仍然左右移动坚持挡在坦克之前。当头坦克停下，后续坦克相继停下。王维林爬上当头坦克，坦克里面的军人爬出来，两人交谈了一会儿，那个军人朝后面的坦克挥挥手。王维林爬下了坦克，坦克往后面退了几步，又突然加速试图冲过他，王维林再次挡在了坦克跟前，相持一段时间后，一个骑自行车的男子骑到了坦克跟前并与王维林交谈了几句，这时路北方跑来两个身穿蓝色工装上衣高举双手的男子，一左一右夹送王维林快速跑步离开坦克，从画面上消失了。录像到此完毕。中央电视台曾播放过相关录像，显示王维林安然从坦克前离开。中央电视台在语言文字的报道中，称王维林为“螳臂挡车的歹徒”。中央电视台在后来大量播放六三六五节目时不再播放这一录像。<br />
&nbsp; &nbsp; 1989年“6&#8226;4”后，中共总书记江泽民接受美国记者芭芭拉&#8226;沃尔斯特访问时，被问及王维林的下落，江泽民表示不知情，至于之后王是否被秘密逮捕并处死，江泽民表示没有(never)。<br />
&nbsp; &nbsp; 1992年台湾清华大学校长沈君山多次会晤江泽民询问王维林下落，江泽民明确回答：死了的人名单里没有他，关着的人里没有他，看来是躲起来了。<br />
&nbsp; &nbsp; 2003年，天安门广场学生指挥部纠察队长张健在马德里对三人说：王维林是安徽合肥工业大学学生，是他任队长的纠察队队员，他很熟悉，尚在人世，居大陆某地。<br />
&nbsp; &nbsp; 2007年，香港某大学黄某在德国柏林说：王维林是考古专业人员，尚在人世，在台湾故宫博物馆任职。<br />
&nbsp; &nbsp; 以上说法莫衷一是，都没有确凿依据。王维林身份至今未能确认，生死未卜。“王维林”这个名字最早出现在英国报刊上（Wang Wei lin），中文是据其音译沿用流传，英文报刊的准确名称则未见，更未见有人顺藤摸瓜，予以挖掘考证。“王维林”的真实姓名至今未有任何确定资料可资佐证。<br />
