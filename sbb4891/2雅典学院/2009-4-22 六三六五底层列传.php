&nbsp; &nbsp; &#8226;白衣少女<br />
&nbsp; &nbsp; 1989年6月4日凌晨3时10分，解放军戒严部队向天安门城楼东侧南向广场上的学生一阵扫射，几个人被打倒了。学生愤怒异常，难以相信。这时，一个身着白色连衣裙的姑娘，在卧倒的人群中站了起来，向军人大步跑去。夜色中，少女长发飘飘，在白色连衣裙衬托下，象一位天使，象一尊观音，身后，还跟着5、6位男青年。全场的人都惊呆了。军人又开枪了，5、6位男青年先被击倒，白衣少女跑到军人10余米处，子弹的射中了她的大腿。她象天使一样折翼倒下。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 【九曲澄曰：她真实存在，有两位目击证人不约而同的叙述了这一场景。她是谁？她是活着的林昭，她是民族的精魂，她是我们的老师，她是我们的姐妹，她是女神，她是观音，她是女娲！找到她不会很难，只要把二十年的时间腾出二十分之一，只要有心，只要协力，我们的白衣少女就会来到我们中间。】<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; &#8226;挥舞旗帜遭坦克碾压者<br />
&nbsp; &nbsp; 6月4日凌晨，聚集在金水桥一带的坦克装甲车开始向广场驶来，数千人的队伍在学生纠察队手拉手的维护下，向广场东南角退去。有一位男生爬上帐篷顶，对着迎面而来的坦克挥舞着一面旗帜，一直挥舞着，直到坦克碾过帐篷和他的身躯。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; &#8226;一位北京市民<br />
&nbsp; &nbsp; 1989年6月3夜至4日上午，他独自用平板车冒着遭解放军戒严部队枪击的危险，拉了21趟受伤者。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; &#8226;帐篷中的殉道者<br />
&nbsp; &nbsp; 6月4日凌晨4时，天安门广场突然熄灯后不久，列阵于金水桥前解放军戒严部队的坦克、装甲车向广场推进，矗立在广场北端中央的“民主女神”象首当其冲，轰然倒下，然后是一座座帐篷被碾压而过，成为一滩滩鸡零狗碎，连同帐篷内疲惫不堪尚在梦乡的学生。坦克的轰鸣掩盖了学生惨死的凄厉叫声。其时，在天安门广场纪念碑南北两侧有300余座帐篷，“民主女神”塑像周围有20座帐篷。有充分的证据证明帐篷里有学生，有充分的证据证明坦克一径碾压帐篷而]有军人事先搜查驱离帐篷中的人员。（参阅吴仁华著《天安门广场血腥清场内幕》392－402页）。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; &#8226;两位女青工。<br />
&nbsp; &nbsp; 6月4日凌晨1时许，一位浑身是血的青年工人来到天安门广场人民英雄纪念碑特别纠察队处泣不成声报信：与他同行的工人弟兄都被解放军戒严部队打死了，他是唯一的生存者。这时，这支工人纠察队仅存的坐在纪念碑底座最高层的两位女青年猛地甩掉披在身上的军大衣，情绪极为激动地要与来报信的男青年一起去西长安街。特别纠察队领队、政法大学教师吴仁华和队员流泪苦劝苦阻，坚决不放这两位年青的女同胞去拦阻全副武装的解放军戒严部队军人、装甲、坦克，去送死。“求求你们俩，别去了！千万别去了！我们良心上受不了！受不了呀！”两位女青年扑通一下跪倒在地，哭着说：“同行的弟兄们都死了，我们不能贪生不去啊……”。这两位女青年还是随着浑身是血的男青工走了。没见他们再回来。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; &#8226;喊“中国人，站起来！”的北京市民<br />
&nbsp; &nbsp; 6月4日凌晨4时25分许，一群北京市民从天安门广场东南角向人民英雄纪念碑底座走来，一边走一边齐声呼喊着：“中国人，站起来！中国人，站起来！”其时，解放军戒严部队在北京城内各地和天安门广场开枪杀戮已是尽人皆知的血腥事实，广场剩有数千名学生坚守着。这群北京市民慷慨赴难，喊声激昂悲壮深刻，在漆黑的夜空回荡，似无声处惊雷。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; &#8226;温州媛子<br />
&nbsp; &nbsp; 6月4日凌晨4时半许，天安门广场人民英雄纪念碑底座来了一位温州媛子（温州方言，姑娘叫“媛子”），坐在了特别纠察队领队、政法大学教师吴仁华的旁边。因系不认识的陌生人，在时刻有生命危险的杀场环境中，吴不免追问其来历，媛子答是搞摄影的个体户，温州人，要将这百年难遇的历史事件用镜头记录下来。恰吴仁华也是温州人，二人皆兴奋地改用乡音交谈。温州媛子为吴仁华在纪念碑前拍了一张照片，说会将照片寄给他，吴仁华给了她家乡的地址。此后，吴仁华未再见到这位温州媛子，也没收到过她寄的照片。温州媛子，您在哪里？愿您平安！<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; &#8226;燃篝火视死而归者<br />
&nbsp; &nbsp; 6月4日凌晨4时，解放军戒严部队在天安门广场全部熄灯。风高放火，月黑杀人。此举给尚守护在广场的数千学子产生了巨大的死亡恐怖心理压力。这时，在广场纪念碑底座西侧20余米处，有学生用棉被帐篷布等物燃起了第一堆篝火，随后，在纪念碑周围陆续燃起了好几堆篝火。燃篝火者不会不懂这是最好的靶标。有学生特意跑向篝火，围绕着篝火席地而坐。闪烁的篝火，映衬着纪念碑底座周围一面面迎风飘扬的校旗和一张张视死而归的殉道者面孔。<br />
