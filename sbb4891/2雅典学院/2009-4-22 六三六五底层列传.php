&nbsp; &nbsp; &#8226;方政<br />
&nbsp; &nbsp; 方政，北京体育学院理论运动生物力学专业学生，1966年出生于安徽合肥。1989年6月4日晨，方政从天安门广场随着撤退的学生队伍走到离中南海约50米的六部口附近。三辆随后追来的坦克施放的毒气弹在他身后爆炸。跟谁方政的一位女同学吓坏了，方政扶抱她往人行道移动。这时一辆坦克从方政身后急速驰来，坦克炮筒子就在方政头上。方将学妹推靠在自行车道与人行道的护栏上，自己躲闪不及倒地，坦克压到双腿，毫不迟疑地碾了过去，方正的裤子连腿被卷到坦克履带的链条上，拖了一段路后，链条把双腿撕拉掉了，方政从坦克的履带上掉了下来。方政还有知觉，下意识地滚到了路边，靠在护栏上，昏死了过去。好心人将方政送积水潭医院救治。已经接近死亡的方政由于医生护士的全力抢救得以存活，右腿截肢大腿上部，左腿截肢膝盖位置。<br />
