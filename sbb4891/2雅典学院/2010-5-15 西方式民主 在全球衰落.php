													<h2>西方式民主 在全球衰落</h2>
						
						
																											<div id="postmessage_617466" class="t_msgfont">●美媒：●西方式民主●在全球衰落<br />
<br />
2010年3月22日 10:30<br />
来源:新华网 选稿:陆婷婷 <br />
<br />
　　东方网3月22日消息：美国《新闻周刊》3月22日发表文章《民主是如何消亡的》。作者认为，近年来，西方式民主在非洲、拉美、中东和前苏联地区急剧衰落，因为它未能促进经济繁荣，也没给民众带来福祉。而在俄罗斯和中国，大多数人满意国家现状，领导人的民意支持率之高，西方人只有做梦才能得到。<br />
<br />
　　20世纪90年代和本世纪初，政治自由在发展中国家蓬勃发展。尽管在20世纪90年代，独裁主义者仍统治著非洲、东欧和亚洲的大部分地方，但到2005年，民主国家在这几个大洲四处出现。随著萨达姆倒台，塔利班被推翻，土耳其的军事干政明显退出以及伊朗改革派总统哈塔米掌权，就连民主改革长期滞后的中东似乎也添加了这个潮流。2005年，美国自由之家指出，只有9个国家经历了民主政治的倒退，而在其2009年的报告中，该组织称，&quot;非洲、拉美、中东和前苏联40个国家&quot;的民主衰落了。该组织发现，举行选举的民主国家的数量跌回116个，是1995年以来数量最少的。<br />
<br />
　　民主衰落的原因可能会让人感到吃惊。如今，许多曾帮助将独裁者赶下台的中产阶级，感到要创建民主国家是多么困难，并且向往独裁时期。为什么会出现这种情况？在许多案例中，这是由於20世纪90年代出现的年轻民主国家的早期领导人未能认识到自由社会需要强大的制度、执政党的坚定反对派以及妥协的意愿。他们获胜后，就会利用所有权力工具主宰国家，并把好处分给支持者或自己所属的部落。在许多国家，这种对民主的狭隘理解不仅歪曲了这个词的真正含义，还使公众疏远。他们开始厌恶这些看起来与其独裁前任一样、对公众福祉不闻不问的民主人士。<br />
<br />
　　与此同时，小布什政府将伊拉克战争与促进民主联系在一起，这玷污了许多人(尤其是中东许多人)对民主化的看法。<br />
<br />
　　全球经济危机也使民主的魅力下降。对许多发展中国家中产阶级来说，民主的传播是与资本主义的传播联系在一起的，因为其中许多国家在接纳政治自由的同时也会开放经济。随著经济危机使人们的收入减少，许多人认为经济陷入低迷在一定程度上是民主造成的。多明尼加总统费尔南德斯在2008年拉美领导人峰会上声称：&quot;对这个地区(拉美)民主前景的期待让位於幻灭，因为民主未能促进经济繁荣。&quot;<br />
<br />
　　结果，在几乎所有大洲，民主都在逐渐消亡。在伊拉克，萨达姆之后的首批领导人，依靠最粗鲁的恐吓手段击败敌人并上升到政治体系的最高端，使人民大失所望。在最近的伊拉克选举中，投票率较2005年选举时下降，尽管有大量广告鼓励人们投票。在菲律宾总统阿罗约统治下，安全部队绑架和杀害左翼活动人士的事件日益增多。在柬埔寨，首相洪森及其领导的政党在对他们百依百顺的法庭上以诽谤罪控告他人，据称还殴打和杀害活动人士以实现对政治体系的完全控制，尽管他们也是经选举上台的。而在许多非洲国家，所谓改革者，如肯亚现任总统齐贝吉，在发誓促进真正的政治自由后上台，但很快就只是利用自己的职位打击对手和照顾本族的支持者。<br />
<br />
　　这种现象最明显的例证之一就是泰国。20世纪90年代，泰国被许多人视为世界上前途最光明的年轻民主国家之一。2001年，乘著重创泰国的亚洲金融危机过后民众普遍不满的风潮，身为亿万富翁的电信巨头他信承诺重振经济并对在全国占大多数、但在历史上被身为精英阶层的泰国政治家不屑一顾的穷人实施社会福利计划，从而在全国大选中获胜。他信一就职就兑现了他许下的一些民粹主义承诺：他的政府启动了全民医保计划，向每个村庄发放贷款以推动经济增长。这位总理进行了一次经过精心策划的、倾听穷人声音的政治秀，拜访一个又一个村庄，倾听哪怕最为琐碎的抱怨。<br />
<br />
　　然而，他信并非像他一开始表现的那样是泰国民主的福音。反而，他开始破坏泰国许多年轻的民主制度。他摧毁了行政机构和司法系统，用自己的亲信替换独立思考者，据称还让支持者收购媒体集团，然后压制批评性报道，从而让媒体闭嘴。在泰国宣布&quot;对毒品开战&quot;后，国内外人权组织指责他信宽恕安全部队违反法律程序从事谋杀和致人失踪的行为。在缉毒战争期间，共有2500多人神秘死亡。<br />
<br />
　　这种攫取权力的行为最令人讨厌的影响之一就是，在许多近来民主出现倒退的国家，曾经推动政治自由的中产阶级现在也诉诸未经法律许可的、不民主的策略。例如，泰国中产阶级城市居民对他信滥用权力深感失望，担心他会牺牲他们，授权给穷人。他们没有通过民主程序与他信对抗，而是通过关闭政府机构和要求发动军事政变来破坏民主。为了首先将他信、接著将他的支持者赶下台，成群的抗议者在2006年、2007年和2008年试图使曼谷陷入瘫痪，对议会发动包围，并在2008年接管了主要机场。许多人要求进行军事干预，或实行其他类型的良性独裁，以恢复法治并打击腐败。他们声称，在他信统治时期，法治状况恶化了，腐败也更加严重。泰国精英阶层得到了他们想要的东西：他信流亡海外，他的反对者当权，而泰国的民主被摧毁了。<br />
<br />
　　其他地方也出现了类似的模式。在菲律宾，上一代人曾为打倒独裁者马科斯而聚集在一起。而马尼拉的中产阶级为了将埃斯特拉达赶下台再次聚集。埃斯特拉达是经民众选举上台的，受到穷人爱戴，却被控大肆贪污受贿。在埃斯特拉达下台后，同一批中产阶级抗议者当中的许多人又试图将阿罗约赶下台。<br />
<br />
　　许多发展中国家中产阶级活动人士对这些选举生成的独裁者感到失望，对年轻的民主国家发生的贪污受贿行为感到灰心。如今，他们甚至渴望回到过去的独裁统治时期。在非洲，毛里塔尼亚和尼日最近发生的军事政变受到城市中产阶级欢迎，而亚洲民主动态调查的数据显示，许多受调查者对他们的民主制度感到不满。俄罗斯的情况也是如此。尽管普京废除了大多数民主制度，但他在中产阶级和其他人群中享有高得惊人的民意支持率。民调显示，大约80%的俄罗斯人说他们对普京的表现感到满意。西方领导人唯有做梦才能享有这样的民意支持率。<br />
<br />
　　中产阶级通过军事政变和其他独裁手段使民主倒退，剥夺了穷人的公民权，这引发了更多的抗议活动。在泰国，成群的抗议者发动了针对试图将他信赶下台的中产阶级的暴力示威。这些抗议者大多是穷人。在菲律宾，贫困男女对中产阶级将他们的英雄埃斯特拉达赶下台感到愤怒，发起他们自己的抗议活动予以还击。如今，随著菲律宾又将举行选举，已出狱并再次参选的埃斯特拉达正获得穷人对其参选总统的支持。这些还击式的抗议活动导致阶级对立，可能需要几代人的时间才能和解。<br />
<br />
　　最大的问题也许是在伊拉克。许多人曾对萨达姆倒台后的生活抱有很高期望，然而，对民主的支持很快就下降了，部分原因是伊拉克中产阶级目睹了新政治体系的混乱和政治内讧。在2003年过后，许多伊拉克中产阶级人士开始渴望重新出现一位独裁统治者，即便是像萨达姆那样残暴的统治者。</div>

							
							
							
							
