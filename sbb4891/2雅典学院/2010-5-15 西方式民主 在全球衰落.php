													<h2>How Democracy Dies</h2>
						
						
																											<div id="postmessage_617487" class="t_msgfont">How Democracy Dies<br />
A global decline in political freedom is partly the fault of the middle class.<br />
By Joshua Kurlantzick | NEWSWEEK<br />
Published Mar 12, 2010<br />
From the magazine issue dated Mar 22, 2010<br />
<br />
<br />
Political freedom blossomed in the developing world in the 1990s and early part of this century. While authoritarians still ruled most of Africa, Eastern Europe, and Asia in 1990, by 2005 democracies had emerged across these continents. The Soviet Union had morphed into Russia, a freewheeling society that seemed to bear little resemblance to its grim predecessor. With the fall of Saddam Hussein, the overthrow of the Taliban, the apparent end of military interventions in Turkey, and the reformist presidency of Mohammad Khatami in Iran, even the Middle East, long the laggard in democratic reform, appeared to be joining the trend. In 2005, Freedom House noted that only nine countries experienced rollbacks of democracy; in its report in 2009, it registered declines in &quot;40 countries in Africa, Latin America, the Middle East, and the former Soviet Union.&quot; Indeed, the organization found that the number of electoral democracies had fallen back to 116, its lowest number since 1995.<br />
<br />
The culprits in democracy's decline may come as a surprise. Many of the same middle-class men and women who once helped push dictators out of power are now seeing just how difficult it can be to establish democracy, and are pining for the days of autocracy. Why has this happened? In many cases because the early leaders of the young democracies that emerged in the 1990s failed to recognize that free societies require strong institutions, a loyal opposition to the ruling party, and a willingness to compromise. Instead, they saw democracy as just semiregular votes; after they won, they then used all tools of power to dominate their countries and to hand out benefits to their allies or tribe. This narrow interpretation of democracy not only distorted the true meaning of the word but also alienated the public in many countries, who became disgusted that these democrats seemed no more committed to the common good than their authoritarian predecessors.<br />
<br />
Too often, Western nations, which after 9/11 refocused their attention from the democratization of the 1990s to the war on terror, said little as democracy went down the drain. Sometimes, the West simply no longer had the time to stand up for democrats abroad. Other times, as in the case of Malaysia and Pakistan, authoritarian rule suddenly benefited the West, since the U.S. could rely on autocrats to help detain terror suspects indefinitely. Meanwhile, the Bush administration's linkage of the war in Iraq to democracy promotion tainted democratization in the minds of many, particularly in the Middle East.<br />
<br />
The global economic crisis has also damaged democracy's appeal. To many middle-class men and women in the developing world, the spread of democracy was linked to the spread of capitalism, since many of these countries opened their economies at the same time as they embraced political freedom. As the crisis cuts into people's incomes, many blame democracy, in part, for the economic downturn. Dominican President Leonel Fernández said as much. &quot;Expectations over the prospects of democracy in the region [Latin America] have given way to disillusion as democracy failed to boost economic prosperity,&quot; he declared at a summit of Latin leaders in 2008.<br />
<br />
The result is that on nearly every continent, democracy is sputtering out. In Iraq, the first post-Saddam leaders relied on the bluntest tools of intimidation to defeat their rivals and rise to the top of the political system, disillusioning the population. In the recent Iraq election, voter turnout dropped from the 2005 poll, despite extensive advertising prodding people to vote. In the Philippines, President Gloria Macapagal Arroyo in 2006 used an emergency decree to, in effect, declare martial law, and her reign has coincided with an increasing number of abductions and killings of left-wing activists by the security forces. In Cambodia, Prime Minister Hun Sen and his party, though also elected, have used libel suits in compliant courts and, allegedly, beatings and killings of activists to gain total control of the political system. In Russia, starting in 2000, Vladimir Putin took advantage of widespread anger at the collapse of Russia's economy in the 1990s to push through changes that crushed any chance for real democracy, replacing elected regional governors with ones appointed by the Kremlin, taking over nearly every independent political party, and neutering most of the media. Venezuela's Hugo Chávez has for more than a decade used his oil wealth to maintain broad popularity with the poor, winning election after election even while turning virtually the entire political establishment into a sycophantic chorus by shutting down independent media outlets, packing prominent state companies with his cronies, and using a national referendum to wipe out his term limits. And in many African countries, so-called reformers, like current Kenyan President Mwai Kibaki, came to power vowing to promote real political freedom but soon used their office simply to crush rivals and favor their own ethnic allies.<br />
<br />
One of the starkest examples of this phenomenon has been Thailand, which was considered by many in the 1990s to be one of the most promising young democracies in the world. Since then it has suffered one of the greatest comedowns. In the 1990s, Thailand passed one of the most progressive constitutions in the developing world, built a vibrant NGO culture that rivaled any in the West, and midwifed an unrestrained media that dug into scandal after scandal. In 2001, riding a wave of popular discontent following the Asian financial crisis, which had decimated Thailand's economy, Thaksin Shinawatra, a billionaire telecommunications magnate, won national elections on a promise to right the economy and bring social welfare programs to the poor, who make up the majority of the country but historically had been treated with disdain by elite Thai politicians. Once in office, Thaksin delivered on some of his populist pledges: his government launched a universal health-care scheme and delivered loans to each village to kick-start economic growth. The prime minister made an elaborate show of listening to the poor, traveling from village to village to hear even the most minor complaints.<br />
<br />
But Thaksin wasn't the boon to Thailand's democracy that he seemed at first. Instead, even as he was extending social protections he set about undermining many of Thailand's young democratic institutions. He gutted the civil service and the judiciary, replacing independent thinkers with cronies, and silenced the media by allegedly having allies buy into media groups and then silence critical reporting. Declaring a &quot;war on drugs,&quot; Thaksin was accused by international and domestic human-rights groups of condoning extrajudicial killings and disappearances by the security forces. Prominent human-rights activists like lawyer Somchai Neelapaichit have simply vanished. Overall, more than 2,500 people died mysteriously during the drug war. Michael Montesano, an expert on Thai politics at the Institute of Southeast Asian Studies in Singapore, says that Thaksin more closely resembles a Latin American caudillo, such as Juan Perón, than a democratic politician.<br />
<br />
One of the unlikely effects of such power grabs has been that in many of the countries where democracy has recently been rolled back, the middle class that once promoted political freedom is now also resorting to extralegal, undemocratic tactics―supposedly to save democracy itself. Middle-class Thai urbanites, for instance, bitterly disappointed by Thaksin's abuses and worried he was empowering the poor at their expense, have rebelled. Rather than challenging Thaksin through the democratic process, such as by bolstering opposition parties or starting their own newspapers, they tore down democracy by shutting down institutions of government and calling for a military coup, even while claiming to support democracy. In order to push first Thaksin and then his allies out of office, mobs of protestors tried to paralyze Bangkok in 2006, 2007, and 2008, launching a siege of Parliament and, in 2008, taking over the main airport, a move that wreaked havoc on travel to the country. Many called for a military intervention or some other kind of benign despotism to restore the rule of law and fight corruption, which they claimed had worsened under Thaksin. &quot;We had to save democracy, even if it meant [ignoring] elections,&quot; said one Thai diplomat sympathetic to the protesters. The Thai elites got what they hoped for: Thaksin is in exile, his opponents are in power, and Thailand's democracy is shattered.<br />
<br />
A similar pattern has played out elsewhere. Middle-class demonstrators in the wealthier eastern part of Bolivia have launched an antigovernment campaign against President Evo Morales, a populist former union leader who has tried to redistribute wealth, nationalize businesses, and use a national referendum to dramatically increase his own powers. In the Philippines, where a previous generation of Filipinos had gathered to bring down the dictator Ferdinand Marcos, middle-class Manila residents came together again to force out Joseph Estrada, popularly elected and beloved by the poor but accused of massive graft. After Estrada left office, many of the same middle-class protestors turned out in attempts to force out Macapagal Arroyo, though she survived and remains in office.<br />
<br />
Disappointed with these elected autocrats and frustrated with the graft of young democracies, many middle-class activists in developing nations are now even longing for the old days of authoritarian rule. In Africa, recent coups in Mauritania and Niger were welcomed by the urban middle class, while data from the Asian Barometer surveys, regular polls that examine Asian attitudes toward democracy, show that many respondents have become dissatisfied with their democratic systems. &quot;Support [in Asia] for authoritarianism is growing rather than diminishing,&quot; argue Yu-tzung Chang, Yunhan Zhu, and Chong-min Pak in an article titled &quot;Authoritarian Nostalgia in Asia.&quot; Such is the case in Russia as well, where Putin, even as he wipes out most of the democratic institutions, enjoys staggeringly high poll numbers from the middle class and other segments of the population. In one recent Angus Reid poll, some 80 percent of Russians said they approved of Putin's performance, ratings that Western leaders can only dream of. <strong>Even in China, where it is the poor in rural areas who now take the lead in protests, the urban middle class appears comfortable with the ruling regime. In a Pew study focused on urban areas, a large majority of Chinese reported that they were happy with national conditions.</strong><br />
<br />
The middle class's push back against democracy, by way of coups and other antidemocratic means, has disenfranchised the poor, sparking still more protests. In Thailand, crowds of protesters, most of them poor, have launched their own violent demonstrations that target the middle classes who tried to push Thaksin out of office. Similarly in Bolivia, the middle-class anti-Morales protesters now have been met with angry pro-Morales protesters mostly drawn from the ranks of the poor. In the Philippines, poor men and women furious that their hero Estrada had been forced out by the middle class launched their own counter-protests. Now, with the nation heading to another election, Estrada, out of jail and running again, is picking up support from the poor for his presidential bid.<br />
<br />
These counterprotests have led to class divides that could take generations to reconcile. After more than a decade of fragile democracy, many institutions created in the 1990s have been destroyed, and those in power have few remaining tools to resolve political tensions. In Russia, for instance, even if a leader came into office who wanted to restore more freedoms, he or she would have to fight the Putinesque system and bureaucracy, which have centralized all power in the Kremlin. In Thailand, even if current Prime Minister Abhisit Vejjajiva wanted to return the country to the freedom of the 1990s, he couldn't, because during Thaksin's rule and then after the coup, Thailand's rulers tore up its reformist constitution, ruined the courts, and so politicized the media that newspapers now slavishly back either the pro- or anti-Thaksin forces. It would take years, if not decades, for a new leader to rebuild the civil service, courts, and other institutions with the type of trained, impartial people who'd been developed before.<br />
<br />
Perhaps the greatest problems are in Iraq, where millions of voters headed to the polls last week for the second time since the fall of Saddam Hussein. Many had high expectations for life after the fall of Saddam, but support for democracy soon dropped off in part because middle-class Iraqis witnessed the turmoil and political infighting of a new political system. At the worst moments of Iraq's post-2003 chaos, when the idea of Iraq becoming a model democracy for the Middle East seemed insane, many middle-class Iraqis, particularly Sunnis, began longing for the return of an authoritarian ruler, even one as brutal as Saddam. In one 2007 ABC News survey, only 43 percent of Iraqis thought democracy would be the best political system for their country. The turnout for the most recent election suggests many Iraqis remain disillusioned. And even if their democracy develops, they can't let down their guard. The history of other young democracies reveals just how fragile this success can be.<br />
<br />
Kurlantzick is fellow for Southeast Asia at the Council on Foreign Relations.<br />
