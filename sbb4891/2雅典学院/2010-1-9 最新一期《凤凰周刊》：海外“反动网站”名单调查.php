													<h2>海外“反动网站”名单调查</h2>
						
						
																											<div id="postmessage_423010" class="t_msgfont">来源：凤凰周刊&nbsp;&nbsp;<br />
<br />
2009年12月14日，一直秉持亲华姿态的新加坡中文媒体《联合早报》网站，突然被屏蔽，原因不明。虽然此后迅速恢复，但业内倾向认为，屏蔽缘于《联合早报》早先对中国加强网络监管以及四川拆迁自焚事件等进行了批评性报道。<br />
<br />
屏蔽是目前中国内地对网络实施监管的主要手段之一，是GFW的主要功能。这种手段一般用于海外的网站，而对于那些建立在内地的网站，一般采取的是直接关闭等手段。<br />
<br />
例 如2009年8月，内地先后对两家微型博客网站“饭否”和TWITTER进行了不同方式的处理。“饭否”因是内地注册，服务器也建立于内地，所以以网站维 护的名义停止了运作。而另一家微型博客网站TWITTER，由于是美国网站，服务器也不在内地，只能通过GFW对其网站进行屏蔽，使内地网民无法登陆该网 站。<br />
<br />
《联合早报》被短暂列入屏蔽网站黑名单时，多维新闻网却已悄悄完成转身。2009年10月8日，中国官方中新网刊载一篇美国总统奥巴 马当选后华裔人士入阁的报道，文中出现“据多维新闻网报道”字样。多维网系国内屏蔽的第一批海外新闻网站，此次突然成为中国官方网站的新闻源，颇耐人寻 味。<br />
<br />
2000年前后，多维一直是点击量最高的海外中文新闻网站之一。虽然长期被屏蔽，但作为一家早期具有重大影响的中文新闻网站，多维一 直是内地网民了解外部世界获取外界信息的重要渠道。近年来，多维新闻开始了“多元化”的“转型”，此举使得多维新闻网的内容与内地新华网的新闻如出一辙。 “转型”的结果，是多维逐渐从内地网民的视野中淡去。<br />
<br />
收购多维的是一家叫做中国数码的香港公司。此公司有位近乎传奇的掌门人―于品海。这 位香港商人的身世与发家史，一直是个谜。而于品海其人，也是在几年前成功收购香港明报集团之后才渐为人知。不过，多维收购事件后，其“前老板”何频称，“ 靠官方在海外建立和收购媒体是一种笨重的办法，只有用更开放的心态面对西方媒体，或者让中国人的民间媒体用专业手法进入西方，才能使中国的形象降低扭曲度 ”。而另一位投资人熊万里，则于三年前低调回国，创办创新型的网站立方网，并获业界瞩目。<br />
<br />
虽然，被收购后的多维网，目前依然属被屏蔽对象，但多少显露出，国内在互联网信息的攻防战中，已经由单纯的建造“围墙”，开始走向在“围墙”外设点主动出击。<br />
<br />
<br />
分离主义组织网站―重中之重<br />
<br />
<br />
所有被屏蔽的网站中，各类政治团体的网站因其大多带有颠覆政府性质，而首当其冲。其中危害最大的，当属分离主义组织的网站。<br />
<br />
建立宣传网站的分离主义组织，除了公众熟知的“藏独”、“疆独”和“台独”组织外，还有绝大多数人闻所未闻的一些分离主义组织，譬如南蒙古独立组织、广西独立组织、满洲国组织、四川独立组织等。<br />
<br />
网 站功能和影响力而言，最大的当属西藏独立组织的网站。西藏分离主义主要组织有“西藏青年大会”（Tibetan Youth Congress）、“国际西藏独立运动”、“国际声援西藏运动”、“自由西藏学生运动”等，它们都有各自的网站。“藏独”组织的网站一般栏目设置极为齐 全丰富，各种相关资料信息也极为齐全，无论在新闻发布、舆论宣传以及网站的形式、内容还有设计上都非常专业。这些网站的英文和中文的水准都达到了非常高的 程度。为显示其政治主张的权威性和准确性，许多西藏分离主义网站还将国内外相关的历史资料、档案和学术著作系统整理翻译成汉语，汇编成集供网民免费下载， 其多年认真经营之功可见一斑。<br />
<br />
各“疆独”组织的网站相对而言就要逊色得多。由于各“疆独”组织的政治诉求和价值理念互有不同，且互相之间 缺少协同合作，与藏独组织的网站相比，看上去网站建设的投入力度明显不足，网站功能设置也极不专业，多半仅有聊具一格之意。各“疆独”组织的网站不但信息 更新频率较低，而且无论是英文还是中文的水准都令人不敢恭维。比如“东土耳其信息中心”的网页里，除了动态新闻以外，相关的历史、资料等栏，虽均有设置， 但内容大都缺失，而且更新不及时，编辑错误比比皆是。<br />
<br />
南蒙古独立组织的网站数量不多，主要有蒙古自由联盟党、内蒙古人民党等组织，网页一般以中文和蒙文为主，相比之下，就更为简陋山寨。不能不说的是，南蒙古独立组织网站上的各种宣传用语以及思维方式，带有强烈的长期受内地系统教育的烙印。<br />
<br />
相 对于这几个组织稍大的网站，其他分离主义网站则多少显得过于“小儿科”，譬如四川独立组织的网站。目前可查到名目的“川独”组织似仅有四川独立运动党。或 许由于该组织成员因长期无法访问被屏蔽网站而导致网站废弃，或是网站拥有者最近改弦更张之故，总之，“川独”组织网站已消失。不过，该组织的部分宣传文章 在其他网站偶尔还可见零星转载。其中某“川独”组织宣传其立国目标时称，他们要建立一个大四川国，为防止“祖国”被中国堵死在西南内陆，他们还将向南征服 缅甸，最后在印度洋上获取一个出海口。这一构想，使得“川独”组织有了几分玩闹色彩。<br />
<br />
“满洲国”官方网站的访问量几乎为零，但也颇显示该 组织颇有经营头脑，他们似乎在以出售“满洲国”护照方式维持网站生计―网站最上面是一条广告：“本政府实施入籍及证明文件新政策，新版护照推出，申请费用 减至USD$7”。该网站最新的信息发布于2009年9月18日，内容为“本政府热烈庆祝满洲事变78周年，促成满洲建国”。<br />
<br />
这个由旅日华人建立的网站虽然山寨，但“满洲国”的政府机构组织却是有板有眼。除了有一位22岁的香港青年担任“皇帝”外，还任命了“首相”等职。<br />
<br />
令人捧腹的是，该组织一方面宣称追求自由民主，一方面又对“皇帝”宣誓效忠，不仅轻易对外许诺“封王赏地”，还在“终战纪念日”悼念“日满两军阵亡将士”。<br />
<br />
在其网站中，还有一条令人喷饭的消息：“本国与大清复国组织建立关系”。不过，本刊未能在网上查到与之建立关系的“大清复国组织”的活动。<br />
<br />
台湾民进党也因其“台独”立场而在屏蔽之列。这一名单包括台湾诸多绿营组织与团体，以及其下辖媒体。<br />
<br />
<br />
政治团体网站――屏蔽之列<br />
<br />
<br />
除上述各种分离主义组织外，一些政治团体不以独立为诉求，但其颠覆性依然不容小视，因此，此类组织的网站也是重点屏蔽打击的对象。<br />
<br />
例 如，在上世纪九十年代成立的中国民主党，就是这样一类的组织。该组织于上世纪九十年代在浙江被取缔，但其网站却一直存在，主要以宣传其政治理念与诉求为内 容。但由于在国内的知名度也几乎为零，访问的人数也少得可怜。而曾长期在境外存在的中国托派组织、“中国共产党特别委员会”等组织，一度在文章更新上颇为 频繁，但目下多半关张。<br />
<br />
此外，网上还曾出现过一些类似“中华XX共和国”或“大中华XX联邦”的网站，通常其元首及国民都只有一个人，网站建成之后几乎就再无更新。<br />
<br />
另外，台湾的中国国民党及其相关网站与旗下媒体也在屏蔽之列。<br />
<br />
令人感到意外的是，对于那些自我标榜为共产主义的网站，被关停或屏蔽的也不在少数。<br />
<br />
据 美国专家Ethan Gutmann在《Who Lost China’s Internet》一文中称，中国最早被屏蔽的网站有三个，其中两个是反政府的站点，另外一个则是毛泽东主义网站。很明显，当局认为这类网站具有潜在的巨 大危害性。以内地众多的毛泽东主义网站为例，这种类型的网站有着一个共同的特征，即自认为在坚持真正的马列主义毛泽东思想，对当前中国内地的改革开放进行 攻击。这些网站除少数之外，多数皆在屏蔽之列。尤其是类似文革网站，以及纪念江青等人的网站。<br />
<br />
登入这类网站，常有种时空倒流之感。网站里 充斥着类似“江青同志战斗在修正主义法庭上”、“文化大革命让中国步入世界前列”、“毛泽东戏耍美国航母”、“纪念伟大的斯大林同志”、“毛泽东是怎样对 待‘钉子户’的”、“修正主义特色化岂能与马列毛主义混同”等文章，以及对当今中国政治经济的诽谤与责难。例如纪念江青的网站中，依然将江青奉为“伟大的 旗手”，认为江青是真正坚持毛泽东思想的伟大人物，将当前中国称为修正主义中国。<br />
<br />
这些网站在某种程度上，具有一定的蛊惑性。网站的创办人和拥趸自诩是“中国真正的左派”，他们习惯于将民众对当前社会生活中的所有不满皆归于“修正主义”的改革开放，以此来为“文革”争辩，以毛泽东为旗号来争夺合法性及政治资源。<br />
<br />
<br />
中文新闻网站的海外生存模式<br />
<br />
<br />
对绝大多数内地网民来说，上网最重要的是获取信息。而新闻媒体作为话语权争夺的目标，新闻类网站受到重点关注自是理所当然。<br />
<br />
新 闻网站主要指海外新闻网站，并主要是中文新闻网站。2009年，海外中文新闻网中著名的多维新闻网及下属媒体，被香港中国数码收购。与此同时，中国领导人 也在不同场合强调“打造中国软实力，争夺国际话语权，建立一批有国际影响力的媒体”。这无疑让人联想到多维新闻网的收购案背后，是否另有背景。<br />
<br />
总 体来说，新闻类网站大致包括两大类：一种是独立的新闻媒体，如BBC中文网、纽约时报、华盛顿邮报、苹果日报、CNN网站、中国事务、开放杂志、新世纪、 中央社、中国时报、华尔街时报、法广中文网、博讯网、多维网等。另一种是有着海外政府或官方背景的媒体网站，如美国之音、自由亚洲电台、美国参考等媒体网 站。<br />
<br />
对新闻网站的屏蔽，主要是针对海外新闻媒体一些不利于中国政府的信息，以防流入境内。<br />
<br />
例如在2009年的“七五”新疆暴乱中，内地官方特许海外媒体对当时的情况进行报道。然而这些报道与视频在海外的新闻网站登出后，马上就被内地其他地区的网民知晓，于是在民众中激起了强烈的民族主义情绪，给新疆事件的善后处理带来了困难，令当局被动。<br />
<br />
这类新闻网站的屏蔽并非一成不变。2008年奥运期间，中国官方对众多海外新闻网站取消了屏蔽，而奥运结束之后，屏蔽又再次开始。这种情形此前即有先例，譬如2001年10月，布什总统参加在上海举行的亚太经合会议时，中国官方一度将《纽约时报》等网站临时解禁。<br />
<br />
此 外，2008年奥运火炬在西方国家传递过程中，因拉萨“3・14”事件、火炬传递屡遭恶意阻拦、西方媒体大肆攻击中国等，一个由中国民间青年建立、专门针 对CNN的网站ANTI-CNN迅即横空出世。这个一度在国内国际成为关注宠儿的网站，在2009年同样屡次无法访问。用代理能访问的结果说明，站方宣称 是因“技术故障”而关机的理由明显不真实。“青年爱国宠儿”有时也是会有过失的。<br />
<br />
<br />
不断扩大的异议网站名单<br />
<br />
<br />
除了新闻类和政治团体的网站，其他还有一些类型的网站也在屏蔽的名单中。<br />
<br />
一是宗教团体的网站，如法轮功的明慧网、“大纪元”、基督教地下教会的网站等。<br />
<br />
其 中法轮功的明慧网和“大纪元”名气最大，资金雄厚。他们不仅在各处设立服务器，并且还不断开发出著名的破“墙”软件“自由门”。“自由门”软件由动态网络 技术公司开发，该公司创建于2002年3月，主要从事突破网络封锁软件的开发，主要软件有“自由门”、动网通。“自由门”软件因其法轮功背景，使用它可浏 览因为政治原因被封锁的网页，却无法浏览国内的部分网站与一些色情网站，并非其宣称的完全自由。但是，除了“自由门”软件，法轮功网站给国内网民留下的印 象并不多，尤其是这几家网站长年奉行“以谎言对抗谎言”的做法，其信誉几乎降到了零。<br />
<br />
还有，由于中国与梵蒂冈尚未建立外交关系，海外基督教、天主教的网站以及内地地下教会的网站都在屏蔽之列。<br />
<br />
二是与国内维权活动相关的网站与个人主页。这方面数量众多，内容庞杂，难以归类。与国内维权的困境相同，有些网站的生存周期仅仅以天计。<br />
<br />
三是国内思想理论传播类的网站与论坛。如“世纪中国”、“宪政论衡”、“思想的境界”等。这类网站往往是因缺少网络运营的资质而被关闭。如“思想的境界”网站，在数次被封后，将服务器搬到了“墙外”，以致访问量一落千丈，几无人访问。<br />
<br />
四是各类国际人权组织网站。如人权观察网站和国际特赦组织及其下属的香港、美国、英国分会等。这类网站往往在人权问题上对中国政府多有非难，并且，其网站的宗旨与中国特色的社会主义有着巨大的冲突，对中国也往往戴着有色眼镜审视。因此被屏蔽也顺理成章。<br />
<br />
<br />
个人平台――屏蔽名单新晋之宾<br />
<br />
<br />
2009年8月，习惯于每天打开电脑就登陆TWITTER的中国网民忽然发现，twitter已经无法登陆。而仅仅在几天之前，国内一家名叫“饭否”的微型博客也以网站维护的名义停止了运作。<br />
<br />
TWITTER 与“饭否”都是一种新型的以微型博客为主要形式的网站，网民们可以通过简单的话介绍自己的生活与爱好，也可进行信息交流。对许多网民来说，TWITTER 被封是不可理解的。他们认为，玩TWITTER和“饭否”根本与政治无涉，也不是登陆海外的“反动网站”，对国家安全构不成危害。而另一些网民则认 为，TWITTER与“饭否”的事，可能与早先发生在伊朗的事件有关。在伊朗，TWITTER成为了“危害国家的工具”。<br />
<br />
这类网站，目前正逐渐成为监管的重点。这种新生的社交类网站往往具有良好的信息交流功能，并且难以监控，因此受到格外关注。如，TWITTER、facebook、“饭否“、”做啥“等。<br />
<br />
由 于新技术的应用，实际已经使每个注册用户都成为了一个个人的发布平台，如TWITTER就对第三方完全开放源代码，使得搭建TWITTER交流平台变得异 常容易，也使得传统的监控方式疲于奔命。比较著名的TWITTER第三方网站有iTweet、dabr、twitiq等，第三方客户端有 destoryTwitter、SeesmicDesktop等。不仅如此，目前新的技术似乎都在向个人化方向迈进，如opera公布的技术，已经可以完 全抛离服务器的束缚，使每台接入网络的个人电脑都成了活动信息服务器。<br />
<br />
正是这种新技术带来的监控困境，使得TWITTER等网站瞬间全球风靡，本来只是交友聊天的微博平台，也忽然在一些国家发挥出新的用法。这使TWITTER在伊朗抗议政府的活动中发挥了巨大作用，更让全球的网民都目睹了倒在血泊中那位美丽的伊朗女孩。<br />
<br />
另 外，新兴的各种视频与图片网站也渐渐成了屏蔽的主要目标之一。最近内地对视频网站的关停以及即将实行的许可证制度，即主要针对这类网站。其主要特点是网民 可自行发布视频与图片信息的网站。如youtube、flickr、youmaker等。这类视频网站上，大量色情内容的确让国内不少未成年人的父母担忧 反感。<br />
<br />
在内地的屏蔽名单上，除了一直被屏蔽的具有特殊意义的网站，如“靖国神社”及某些历史事件相关内容的网站外，一些资料类网站也被列 入，其中最出名的就是中文维基百科。维基百科是一款网民可以自己编辑百科条目内容的新型工具类网站。它通过大量网民不断地义务更新，可以使相关资料在全面 性与精确性上达到一个相当的高度。也正是因为它可以由网民自己编辑，一些关于中国的内容遂有了不同于官方的解释。<br />
<br />
屏蔽名单的最后，就是各种黄赌毒网站。</div>

							
							
							
							
