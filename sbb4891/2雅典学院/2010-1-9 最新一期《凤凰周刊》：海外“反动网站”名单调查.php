　　这个由旅日华人建立的网站虽然山寨，但“满洲国”的政府机构组织却是有板有眼。除了有一位22岁的香港青年担任“皇帝”外，还任命了“首相”等职。<br />
<br />
　　令人捧腹的是，该组织一方面宣称追求自由民主，一方面又对“皇帝”宣誓效忠，不仅轻易对外许诺“封王赏地”，还在“终战纪念日”悼念“日满两军阵亡将士”。<br />
<br />
　　在其网站中，还有一条令人喷饭的消息：“本国与大清复国组织建立关系”。不过，本刊未能在网上查到与之建立关系的“大清复国组织”的活动。<br />
<br />
　　台湾民进h也因其“台”立场而在屏蔽之列。这一名单包括台湾诸多绿营组织与团体，以及其下辖媒体。<br />
<br />
　　政治团体网站――屏蔽之列<br />
<br />
　　除上述各种分离主义组织外，一些政治团体不以立为诉求，但其颠覆性依然不容小视，因此，此类组织的网站也是重点屏蔽打击的对象。<br />
<br />
　　例如，在上世纪九十年代成立的中国民主h，就是这样一类的组织。该组织于上世纪九十年代在浙江被取缔，但其网站却一直存在，主要以宣传其政治理念与诉求为内容。但由于在国内的知名度也几乎为零，访问的人数也少得可怜。而曾长期在境外存在的中国托派组织、“中国共ah特别委员会”等组织，一度在文章更新上颇为频繁，但目下多半关张。<br />
<br />
　　此外，网上还曾出现过一些类似“中华XX共和国”或“大中华XX联邦”的网站，通常其元首及国民都只有一个人，网站建成之后几乎就再无更新。<br />
<br />
　　另外，台湾的中国国民h及其相关网站与旗下媒体也在屏蔽之列。<br />
<br />
　　令人感到意外的是，对于那些自我标榜为共a主义的网站，被关停或屏蔽的也不在少数。<br />
<br />
　　据美国专家EthanGutmann在《WhoLostChina‘sInternet》一文中称，中国最早被屏蔽的网站有三个，其中两个是反政府的站点，另外一个则是毛|主义网站。很明显，当局认为这类网站具有潜在的巨大危害性。以内地众多的毛|主义网站为例，这种类型的网站有着一个共同的特征，即自认为在坚持真正的R列主x毛|思想，对当前中国内地的改革开放进行攻击。这些网站除少数之外，多数皆在屏蔽之列。尤其是类似文革网站，以及纪念江青等人的网站。<br />
<br />
　　登入这类网站，常有种时空倒流之感。网站里充斥着类似“江青同志战斗在修正主义法庭上”、“文化大革命让中国步入世界前列”、“毛|戏耍美国航母”、“纪念伟大的斯大林同志”、“毛|是怎样对待‘钉子户’的”、“修正主义特色化岂能与马列毛主义混同”等文章，以及对当今中国政治经济的诽谤与责难。例如纪念江青的网站中，依然将江青奉为“伟大的旗手”，认为江青是真正坚持毛|思想的伟大人物，将当前中国称为修正主义中国。<br />
<br />
　　这些网站在某种程度上，具有一定的蛊惑性。网站的创办人和拥趸自诩是“中国真正的左派”，他们习惯于将民众对当前社会生活中的所有不满皆归于“修正主义”的改革开放，以此来为“文革”争辩，以毛|为旗号来争夺合法性及政治资源。<br />
<br />
　　中文新闻网站的海外生存模式<br />
<br />
　　对绝大多数内地网民来说，上网最重要的是获取信息。而新闻媒体作为话语权争夺的目标，新闻类网站受到重点关注自是理所当然。<br />
<br />
　　新闻网站主要指海外新闻网站，并主要是中文新闻网站。2009年，海外中文新闻网中著名的多维新闻网及下属媒体，被香港中国数码收购。与此同时，中国领导人也在不同场合强调“打造中国软实力，争夺国际话语权，建立一批有国际影响力的媒体”。这无疑让人联想到多维新闻网的收购案背后，是否另有背景。<br />
<br />
　　总体来说，新闻类网站大致包括两大类：一种是立的新闻媒体，如BBC中文网、纽约时报、华盛顿邮报、苹果日报、CNN网站、中国事务、开放杂志、新世纪、中央社、中国时报、华尔街时报、法广中文网、博讯网、多维网等。另一种是有着海外政府或官方背景的媒体网站，如美之音、自由亚洲电台、美国参考等媒体网站。<br />
<br />
　　对新闻网站的屏蔽，主要是针对海外新闻媒体一些不利于中政府的信息，以防流入境内。<br />
<br />
　　例如在2009年的“七五”新疆暴y中，内地官方特许海外媒体对当时的情况进行报道。然而这些报道与视频在海外的新闻网站登出后，马上就被内地其他地区的网民知晓，于是在民众中激起了强烈的民族主义情绪，给新疆事件的善后处理带来了困难，令当局被动。<br />
<br />
　　这类新闻网站的屏蔽并非一成不变。2008年奥运期间，中国官方对众多海外新闻网站取消了屏蔽，而奥运结束之后，屏蔽又再次开始。这种情形此前即有先例，譬如2001年10月，布什总统参加在上海举行的亚太经合会议时，中国官方一度将《纽约时报》等网站临时解禁。<br />
<br />
　　此外，2008年奥运火炬在西方国家传递过程中，因拉_“3・14”事件、火炬传递屡遭恶意阻拦、西方媒体大肆攻击中国等，一个由中国民间青年建立、专门针对CNN的网站ANTI-CNN迅即横空出世。这个一度在国内国际成为关注宠儿的网站，在2009年同样屡次无法访问。用代理能访问的结果说明，站方宣称是因“技术故障”而关机的理由明显不真实。“青年爱国宠儿”有时也是会有过失的。<br />
<br />
　　不断扩大的异议网站名单<br />
<br />
　　除了新闻类和政治团体的网站，其他还有一些类型的网站也在屏蔽的名单中。<br />
<br />
　　一是宗教团体的网站，如明慧网、“大o元”、基督教地下教会的网站等。<br />
<br />
　　其中明慧网和“大o元”名气最大，资金雄厚。他们不仅在各处设立服务器，并且还不断开发出著名的破“墙”软件“自由T”。“自由T”软件由动态网络技术公司开发，该公司创建于2002年3月，主要从事突破网络封锁软件的开发，主要软件有“自由T”、动网通。“自由T”软件因使用它可浏览因为政治原因被封锁的网页，却无法浏览国内的部分网站与一些色情网站，并非其宣称的完全自由。<br />
<br />
　　还有，由于中国与梵蒂冈尚未建立外交关系，海外基督教、天主教的网站以及内地地下教会的网站都在屏蔽之列。<br />
<br />
　　二是与国内维权活动相关的网站与个人主页。这方面数量众多，内容庞杂，难以归类。与国内维权的困境相同，有些网站的生存周期仅仅以天计。<br />
<br />
　　三是国内思想理论传播类的网站与论坛。如“世纪中国”、“宪政论衡”、“思想的境界”等。这类网站往往是因缺少网络运营的资质而被关闭。如“思想的境界”网站，在数次被封后，将服务器搬到了“墙外”，以致访问量一落千丈，几无人访问。<br />
<br />
　　四是各类国际人嘧橹网站。如人喙鄄焱站和国际特赦组织及其下属的香港、美国、英国分会等。这类网站往往在人辔侍馍隙灾政府多有非难，并且，其网站的宗旨与中国特色的社主x有着巨大的冲突，对中国也往往戴着有色眼镜审视。因此被屏蔽也顺理成章。<br />
<br />
　　个人平台――屏蔽名单新晋之宾<br />
<br />
　　2009年8月，习惯于每天打开电脑就登陆TWITTER的中国网民忽然发现，twitter已经无法登陆。而仅仅在几天之前，国内一家名叫“饭否”的微型博客也以网站维护的名义停止了运作。<br />
<br />
　　TWITTER与“饭否”都是一种新型的以微型博客为主要形式的网站，网民们可以通过简单的话介绍自己的生活与爱好，也可进行信息交流。对许多网民来说，TWITTER被封是不可理解的。他们认为，玩TWITTER和“饭否”根本与政治无涉，也不是登陆海外的“反动网站”，对国家安全构不成危害。而另一些网民则认为，TWITTER与“饭否”的事，可能与早先发生在伊朗的事件有关。在伊朗，TWITTER成为了“危害国家的工具”。<br />
<br />
　　这类网站，目前正逐渐成为监管的重点。这种新生的社交类网站往往具有良好的信息交流功能，并且难以监控，因此受到格外关注。如，TWITTER、facebook、“饭否”、“做啥”等。<br />
<br />
　　由于新技术的应用，实际已经使每个注册用户都成为了一个个人的发布平台，如TWITTER就对第三方完全开放源代码，使得搭建TWITTER交流平台变得异常容易，也使得传统的监控方式疲于奔命。比较著名的TWITTER第三方网站有iTweet、dabr、twitiq等，第三方客户端有 destoryTwitter、SeesmicDesktop等。不仅如此，目前新的技术似乎都在向个人化方向迈进，如opera公布的技术，已经可以完全抛离服务器的束缚，使每台接入网络的个人电脑都成了活动信息服务器。<br />
<br />
　　正是这种新技术带来的监控困境，使得TWITTER等网站瞬间全球风靡，本来只是交友聊天的微博平台，也忽然在一些国家发挥出新的用法。这使TWITTER在伊朗抗议政府的活动中发挥了巨大作用，更让全球的网民都目睹了倒在血泊中那位美丽的伊朗女孩。<br />
<br />
　　另外，新兴的各种视频与图片网站也渐渐成了屏蔽的主要目标之一。最近内地对视频网站的关停以及即将实行的许可证制度，即主要针对这类网站。其主要特点是网民可自行发布视频与图片信息的网站。如youtube、flickr、youmaker等。这类视频网站上，大量色情内容的确让国内不少未成年人的父母担忧反感。<br />
<br />
　　在内地的屏蔽名单上，除了一直被屏蔽的具有特殊意义的网站，如“靖国神社”及某些历史事件相关内容的网站外，一些资料类网站也被列入，其中最出名的就是中文维基百科。维基百科是一款网民可以自己编辑百科条目内容的新型工具类网站。它通过大量网民不断地义务更新，可以使相关资料在全面性与精确性上达到一个相当的高度。也正是因为它可以由网民自己编辑，一些关于中国的内容遂有了不同于官方的解释。<br />
<br />
　　屏蔽名单的最后，就是各种黄赌毒网站。<br />
<br />
　　来源：凤凰周刊<br />
<br />
　　作者:田路</div>

							
							
							
							
