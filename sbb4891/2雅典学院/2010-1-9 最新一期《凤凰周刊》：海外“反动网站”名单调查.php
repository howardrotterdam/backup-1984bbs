													<h2>《凤凰周刊》：海外“反动网站”名单调查</h2>
						
						
																											<div id="postmessage_424776" class="t_msgfont">海外“反动网站”名单调查<br />
<br />
<img src="http://i49.tinypic.com/13z27f9.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
文章摘自《凤凰周刊》2010年第2期封面故事栏目<br />
<br />
2009年12月14日，一直秉持亲华姿态的新加坡中文媒体《联合早报》网站，突然被屏蔽，原因不明。虽然此后迅速恢复，但业内倾向认为，屏蔽缘于《联合早报》早先对中国加强网络监管以及四川拆迁自焚事件等进行了批评性报道。<br />
<br />
屏蔽是目前中国内地对网络实施监管的主要手段之一，是GFW的主要功能。这种手段一般用于海外的网站，而对于那些建立在内地的网站，一般采取的是直接关闭等手段。<br />
<br />
例如2009年8月，内地先后对两家微型博客网站“饭否”因是内地注册，服务器也建立于内地，所以以网站维护的名义停止了动作。而另一家微型博客网站TWITTER，由于是美国网站，服务器也不在内地，只能通过GFW对其网站进行屏蔽，使内地网民无法登陆该网站。<br />
<br />
《联合早报》被短暂列入屏蔽网站黑名单时，多维新闻网却已悄悄完成转身。2009年10月8日，中国官方中新网刊载一篇美国总统奥巴马当选后华裔人士入阁的报道，文中出现“据多维新闻网报道”字样。多维网系国内屏蔽的第一批海外新闻网站，此次突然成为中国官方网站的新闻源，颇耐人寻味。<br />
<br />
2000年前后，多维一直是点击量最高的海外中文新闻网站之一。虽然长期被屏蔽，但作为一家早期具有重大影响的中文新闻网站，多维一直是内地网民了解外部世界获取外界信息的重要渠道。近年来，多维新闻开始了“多元化”的“转型”，此举使得多维新闻网的内容与内地新华网的新闻如出一辙。“转型”的结果，是多维逐渐从内地网民的视野中淡去。<br />
<br />
收购多维的是一家叫做中国数码的香港公司。此公司有位近乎传奇的掌门人――于品海。这位香港商人的身世与发家史，一直是个谜。而于品海其人，也是在几年前成功收购香港明报集团之后才渐为人知。不过，多维收购事件后，其“前老板”何频称，“靠官方在海外建立和收购媒体是一种笨重的办法，只有用更开放的心态面对西方媒体，或者让中国人的民间媒体用专业手法进入西方，才能使中国的形象降低扭曲度”。而另一位投资人熊万里，则于三年前低调回国，创办创新型的网站立方网，并获业界瞩目。<br />
<br />
虽然，被收购后的多维网，目前依然属被屏蔽对象，但多少显露出，国内在互联网信息的攻防战中，已经由单纯的建造“围墙”，开始走向在“围墙”外设点主动出击。<br />
<br />
 <br />
<br />
分离主义组织网站――重中之重<br />
<br />
所有被屏蔽的网站中，各类政治团体的网站因其大多带有颠覆政府性质，而首当其冲。其中危害最大的，当属分离主义组织的网站。<br />
<br />
建立宣传网站的分离主义组织，除了公众熟知的“藏独”、“疆独”和“台独”组织外，还有绝大多数人闻所未闻的一些分离主义组织，譬如南蒙古独立组织、广西独立组织、满洲国组织、四川独立组织等。<br />
<br />
就网站功能和影响力而言，最大的当属西藏独立组织的网站。<br />
<br />
西藏分离主义主要组织有“西藏青年大会”（Tibetan Youth Congress）、“国际西藏独立运动”等，它们都有各自的网站。“藏独”组织的网站一般栏目设置极为齐全丰富，各种相关资料信息也极为齐全，无论在新闻发布、舆论宣传以及网站的形式、内容还有设计上都非常专业。这些网站的英文和中文的水准都达到了非常高的程度。为显示其政治主张的权威性和准确性，许多西藏分离主义网站还将国内外相关的历史资料、档案和学术著作系统整理翻译成汉语，汇编成集供网民免费下载，其多年认真经营之功可见一斑。<br />
<br />
各“疆独”组织的网站相对而言就要逊色得多。由于各“疆独”组织的政治诉求和价值理念互有不同，且互相之间缺少协同合作，与藏独组织的网站相比，看上去网站建设的投入力度明显不足，网站功能设置也极不专业，多半仅有聊具一格之意。各“疆独”组织的网站不但信息更新频率较低，而且无论是英文还是中文的水准都令人不敢恭维。比如“东土耳其信息中心”的网页里，除了动态新闻以外，相关的历史、资料等栏，虽均有设置，但内容大都缺失，而且更新不及时，编辑错误比比皆是。<br />
<br />
南蒙古独立组织的网站数量不多，主要有蒙古自由联盟党、内蒙古人民党等组织，网页一般以中文和蒙文为主，相比之下，就更为简陋山寨。不能不说的是，南蒙古独立组织网站上的各种宣传用语以及思维方式，带有强烈的长期受内地系统教育的烙印。<br />
<br />
相对于这几个组织稍大的网站，其他分离主义网站则多少显得过于“小儿科”，譬如四川独立组织的网站。目前可查到名目的“川独”组织似仅有四川独立运动党。或许由于该组织成员因长期无法访问被屏蔽网站而导致网站废弃，或是网站拥有者最近改弦更张之故，总之，“川独”组织网站已消失。不过，该组织的部分宣传文章在其他网站偶尔还可见零星转载。其中某“川独”组织宣传其立国目标时称，他们要建立一个大四川国，为防止“祖国”被中国堵死在西南内陆，他们还将向南征服缅甸，最后在印度洋上获取一个出海口。这一构想，使得“川独”组织有了几分玩闹色彩。</div>

							
							
							
							
