国际在线&#8226;腾讯评论：2009年是互联网的多事之秋。从工信部推行绿坝系统到个人网站被关停；从魔兽世界风波到BT网站被叫停；以及贯穿全年的反低俗整治互联网活动等…这些政策的变化，对您造成了怎样的影响？<br />
<br />
陈志武： 广电总局近日发放首批3G手机视听牌照，包括央视、上海文广、人民日报、新华社、中国国际广播电台、央广视讯、视讯中国、华夏视联这八家与广电关联的企业入选。而多家民营视频网站公司却没有。这是政府部门既做裁判又做球员赢利后会带来的典型后果，再次说明有国企加入的行业、特别是跟行政主管部门有利益关系的国企介入的行业，不太可能有民企和国企公平竞争，不太可能有民企和国企在规则和机会面前的人人平等。许多经济学家同仁，到今天还认为，企业是国有还是民有不重要，说关键是经济是否为市场经济。―― 我要问，在一个由国企享受机会牌照特权、融资特权、法律规则特权的社会里，其经济怎么可能是市场经济？希望有更多人能多多思考国家拥有并经营企业的方方面面后果，包括对法治文化的侵蚀、对权力与权利平衡点的影响。<br />
<br />
行政垄断抑制竞争和创新，使建设创新型国家的愿望变得渺茫。原来很多人以为之所以中国不能成为创新型国家，是因为没有钱、没有研究经费，可是，现在钱有了，研究经费也投入得越来越多，但还是离创新型国家遥远，为什么？行政垄断是症结之一，经费和机会都给了那些受到特权保护的机构和国企，创新动力当然稀缺。<br />
<br />
对中国来说，互联网行业过去因为没有国企的介入而成功的经验，应该值得向所有其它传媒行业推广，特别是应该在各种新型媒体推广。从一开始，中国的互联网是由搜狐、新浪、网易、百度、腾讯等民营公司做起来的，充满了相对公平的竞争，没有哪家拥有国家背景的特权，所以，中国的互联网行业到今天才这么有活力，不仅给年轻人提供大量就业机会，给社会和经济做出巨大贡献，而且也让中国互联网行业开始引领世界新潮流。这种成功模式不是活生生的、自主产生的吗？为什么不被推广到像手机视听这样的新型传媒呢？</div>

							
							
							
							
