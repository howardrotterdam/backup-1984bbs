<br />
　　自从看到有些人在21世纪连戊戌变法都无法接受的时候，就再也没有兴趣与他们辩论了。如果与他们辩论，就是把自己降低到他们的水平之上了。跪在地上甚至趴在地上的人，无法理解那个伟大的时代。没有追求真理的精神，没有舍生取义的勇气，探索出来的结果不过是吃亏占便宜的道理，不过是怎样让自己合适而已。<br />
