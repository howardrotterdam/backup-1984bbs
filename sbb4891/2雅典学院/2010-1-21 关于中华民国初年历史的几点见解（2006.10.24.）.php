																											<div id="postmessage_444809" class="t_msgfont">六、 于孙中山先生<br />
<br />
　　近年来随着舆论的多元化和对历史文献的研究，对孙中山先生的非议有所增加。应该这样认为，历史的真相是应该探求的，不论这种探求会得出什么样的结果。同时也要看到，孙中山先生曾经作为反对专制的象征是不争的事实。<br />
<br />
　　批评者们忽略了这样一个问题：孙中山自从向李鸿章上书失败以后就不再相信满清政府，转而走上了革命的道路。作为革命者，他经历了不少失败，也做过错误的决策。但是，后来的事变证实了他的方向是正确的。要么是孙中山先生先知先觉，要么是他的预言恰巧应验了。不论是哪一种原因，能够成为变革世道中的领袖人物都应该有主见、坚持和人格力量。就是在这种看似一定要失败的人中间，产生了伟人。瞻前顾后的人，见风使舵的人，期待圣主隆恩的人，注定成不了领袖。<br />
<br />
　　最终成就孙中山先生领袖地位的恰恰是满清朝廷。历史给了他们太多的机会，而他们却注定与未来无缘了。如果他们少犯几个错误，孙中山先生就会作为摇旗呐喊的反对派领袖在海外终其一生。<br />
<br />
　　孙中山先生是一位思想家。他有很多卓越的见解，同时也有多变的缺点。他一生追求真理，又在晚年多少偏离了初衷。他提出的三民主义和五权分立，包含了宝贵的创造。但是在现实中，三民主义像苏联的“无产阶级民主”一样，成为拒绝民主普世价值的理论；而五权分立缺少可操作性，弱化了真正需要坚持的三权分立。至于北伐战争，将在后面讨论。<br />
<br />
　　我不反对以政治家或者有些人所说的政客的角度，甚至以个人的角度研究孙中山先生，不反对指出他的某些行动的党派动机甚至个人动机。但是，如果武断地认定孙中山先生的行动大多是为了私利，同时又根据一些官文认定清廷和袁世凯的行动大多是为了公益，是不公正的，无助于揭开历史的真相。<br />
<br />
　　七、关于北伐战争<br />
<br />
　　辛亥革命胜利以后，旧的政治势力掌握了权力。一般来说，社会的变革总是要伴随着政治人物的更替。在正常的情况下，随着选举的实行，在政治版图上总可以达到一定的平衡。但是，宋教仁率领国民党赢得国会选举的胜利以后，被人暗杀于上海火车站。应该说，宋教仁之死堵住了政权和平更替的大门。<br />
<br />
　　随后发生的二次革命似有可商榷之处。在国民党尚占据国会多数，和平手段还可以利用以及军事实力悬殊的情况下仓促举事，造成了革命力量的削弱，给了袁世凯镇压进步势力的口实。<br />
<br />
<br />
　　随后的民国政府越来越失去国民中间活跃分子的支持。袁世凯称帝，军人干政，军阀混战，曹锟贿选，极大地削弱了政府的合法性。<br />
<br />
　　孙中山先生南下护法具有某些实质上的正义性，在策略上也有可取之处，但仍然属于军事割据。<br />
<br />
　　对于北伐战争，至少从后果上应该予以否定。国民党战争之后不但没有建立民主，反而实行了比北京政府还要极端的军事独裁。就这样，国民党走向了自己的反面。为此，李宗仁在回忆录中指出，这是以俄为师的结果。他批评将介石“全仿极权国家之体制，而全无极权国家之效能。”应该说，李宗仁的总结是精辟的。<br />
<br />
　　孙中山先生在北伐之前抱病前往北京谈判的历史，也值得探讨。<br />
<br />
　　八、 关于“五四”运动<br />
<br />
　　近年来常有对于“五四”运动的批评。我认为，“五四”对于中国传统文化的冲击虽然有可商榷之处，但不清理旧的文化，社会是难以进步的，欧洲的文艺复兴和启蒙运动证明了这一点。<br />
<br />
　　我在《儒学问题论纲》中这样说：<br />
<br />
　　一般说来，大多数社会都有自我修复的能力，传统文化又有顽强的生命力，所以传统就延续下来了。在中国，也经历过这种文化传统的修复的过程。“五四”新文化运动的领袖人物胡适，后来就给予孔子较为积极的评价。在政治层面，蒋介石一直提倡孔孟之道；刘少奇在《论共产党员的修养》一书中阐述了不少儒家的道理；而周恩来受到的欢迎，也是与他表现的某些儒家风范分不开的。<br />
<br />
　　中国最大的教训是毛泽东在彻底革命的口号之下，以个人的意志代替了国家和社会的需要。他把中国传统文化当作对立物，不断地、全面地、系统性地加以摧毁。更为严重的是，破坏传统的同时，还全面地对抗世界潮流。<br />
<br />
　　这就是我对这个问题的解答。</div>

							
							
							
							
