																											<div id="postmessage_444812" class="t_msgfont">九、 关于一些民国历史人物<br />
<br />
　　1.康有为&nbsp;&nbsp;<br />
　　伟大的变革先行者。国人为甲午战争的失败而惊恐的时候，他与先知先觉者一起，试图为中国找到新的道路，并且协助光绪皇帝进行变法。近来有人批评他的人品和办事风格，改革者应该引以为戒。流亡日本期间，仍然坚持君主立宪，对光绪皇帝的忠心值得肯定。民国时期参与旧王朝复辟，是叛逆行为。如果为他辩护，可以说他是满清王朝的忠臣，而这个王朝已经被国民废弃了。<br />
<br />
<br />
　　2.袁世凯&nbsp;&nbsp;<br />
　　袁世凯曾经是维新派人物，在戊戌变法时又背叛光绪皇帝，向慈禧太后告密。1911年辛亥革命爆发以后，他迫使清廷退位，为避免南北战争做出了贡献。此后在大总统职位上的作为，应该以客观的态度评价。至于称帝复辟帝制，是不能原谅的篡逆罪行。<br />
<br />
　　3.黎元洪&nbsp;&nbsp;<br />
　　早期民国政治家。武昌起义时在革命党的胁迫之下参加革命，任鄂军总督。与革命党人一起坚守，为南方革命力量的兴起赢得了时间。以后任民国副总统，忠于共和，反对袁世凯称帝。袁世凯去世之后，继任民国大总统。黎元洪与国务总理段祺瑞的“府院之争”固然有对民国约法中权力界定的分歧和权力之争的因素，但更重要的是他坚持约法的精神，反对军人干政。黎元洪并非革命党人，但投身共和以后就不再后退，作为副总统和大总统坚守对于国民的誓言。他没有鲜明的个性，没有军队的支持，也没有什么党派和团体的支持与约束，但遇到危险仍然坚持原则。因此，他的政治品质在辛亥以来的政治家中罕见，值得称赞。后世有人认为黎元洪投机，是和事老，是傀儡，这样的评价是不公平的。<br />
<br />
　　4.宋教仁&nbsp;&nbsp;<br />
　　伟大的宪政先行者。率领国民党赢得国会选举的胜利，随后被人暗杀于上海火车站，年仅32岁。温和的宋教仁不如容于华夏，这是宋教仁的失败，更是整个民族的失败。<br />
<br />
　　5.梁启超&nbsp;&nbsp;<br />
　　政治活动家和思想家。参与戊戌变法，以后与康有为一起流亡日本。民国初年在政治上活跃。我记得有学者说，民国时期有人对梁启超有所非议，称其多变：先忠于朝廷，又协助袁世凯，以后又策划倒袁。我认为，这些变化有道义上的依据。他是一位思想家，给了我们很多启迪。他早期激进，晚年趋向保守。他以保守的一面，告诉我们很多想不到的问题。比如，他认为中国人“无高尚之目的”。我们看一看不少人在民国初年的表现，在大跃进中的表现，在文革中的表现，以及现在表现，就会折服他的一言以蔽之。同时，他的有些研究进入了闭门造车。比如他提出了“无产阶级和无业阶级”的说法，即政治家是无业阶级，应予取消。此文最近有人转贴，获得好评。但虽然雄辩，却不切实际。总的来说，他的思想值得研究。<br />
<br />
　　6.鲁迅&nbsp; &nbsp;<br />
　　思想家和文学家。他的独立精神、批判精神以及对于人性的洞察无人出其右。现在他遭到不少非议，例如只破坏不建设，全盘否定传统，心里阴暗，等等。我认为社会不能没有批判精神，鲁迅就扮演了这样的角色。鲁迅指出的问题国人现在不但仍然具有，而且变本加厉。只要国人仍然如此，鲁迅就不会过时。一些人试图证明国人的缺点是合理的，问题在于鲁迅多事。打倒了批评者，一切就万事大吉了。<br />
<br />
　　7.胡适&nbsp;&nbsp;<br />
　　思想家和教育家。一生坚持自由主义和民主宪政。在教育和治学上有独到和务实的方法，影响了一代学者。他的思想缺少独创性，但在动荡的世道里坚持了最重要的原则和精神。<br />
<br />
　　再谈一谈鲁迅与胡适的比较。现在批评鲁迅的人，也还在用鲁迅的风格批评时事；现在用类似胡适的方法解释问题的人，常常会遭到抨击。这说明，民主宪政和自由主义也不能脱离批判精神，不能回避紧迫的问题。批评鲁迅的人可以说，一味地反对最容易；而批评胡适的人则认为，重复自由主义而回避具体问题最安全。<br />
<br />
　　我认为，如果认真地学习鲁迅的著作，就会了解要建设什么。同时我也认识到，作为“鲁迅派”，观点可能有不客观之处。看到很多“鲁迅没有建设性”的批评，促使我注意批评的分寸和提出具体的建议。<br />
<br />
　　8. 将介石 <br />
　　孙中山先生的追随者，为辛亥革命做出过贡献。在北伐战争的末期以军队领导人身份夺取党政权力，延续了军人专政的历史。对于执政期间的专制和侵犯人权的事例，应予谴责。对于执政期间的成就与不足，应该以客观的态度讨论。将介石领导抗日战争的功绩，应予以颂扬。作为领袖人物，将介石最大的缺点是专横而且不能抓住重要的问题。例如在抗战前夕，面对强大的抗日舆论无动于衷，使自己处于被动的境地，至少是公共关系上的失策。抗战胜利后，漠视国民对于反对腐败和土地改革的要求，以致于拥有丰厚的政治资本和军事力量而输掉了国内战争。<br />
<br />
　　9.汪精卫&nbsp;&nbsp;<br />
　　国民革命的战士。曾经试图刺杀摄政王失败而入狱，在狱中大义凛然。被孙中山先生所信任，为遗嘱撰稿人。反对将介石在北伐当中的军事独裁，并且一度反对国共分裂，以后又试图改组国民党。汪精卫有温和而务实的政治主张，不幸在乱世中无法实现。可以想象，汪精卫一定有强烈的获得成功的愿望。对于后来投靠日本而成为汉奸，不能予以翻案。他在1938年底出走，1940年成立伪南京政府，距离抗战开始仅仅一到三年。汪精卫的叛国投敌，对于抗战是极大的打击。</div>

							
							
							
							
