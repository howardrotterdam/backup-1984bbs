																											<div id="postmessage_444818" class="t_msgfont">十、 一些值得研究的题目<br />
<br />
　　1.孙中山让位于袁世凯的因果和细节&nbsp;&nbsp;<br />
　　孙中山让位是出于无私，还是迫不得已，或者二者兼而有之？革命党内力促孙中山辞职者有谁？他们以后怎样回顾这段历史？<br />
<br />
　　2.民国初年国会的运作、党派和辩论&nbsp;&nbsp;这种关于具体操作的研究，将会有利于未来。<br />
<br />
　　3.第一次国会选举的程序、选民资格、公众参与程度和公正性。<br />
<br />
　　4.早期民国历任政府的作为、政绩以及施政的得失。<br />
<br />
　　5.具体政治人物与政治结论无关的具体行为&nbsp;&nbsp;<br />
　　例如有人指出的段祺瑞在经济问题上的廉洁。至于近年来广泛流传的“段祺瑞因三一八惨案长跪谢罪，并且从此吃素”的说法，就有必要核实。还有，吴佩孚曾经表现的开明和左倾，以及他的某些正义感。核对每一个细节，才能够接近真相。<br />
<br />
　　6.关于陈炯明与孙中山之争<br />
　　对于陈炯明与孙中山之争，过去和现在有截然不同的说法。对于这个问题，我确实不了解。而对于新的说法，由于感到有些文章在材料的引用上有片面和武断的而不敢采纳。争端是否由于陈炯明坚持联省自治和联邦制而起，我期待着客观、全面一些的研究。<br />
<br />
　　7.关于孙中山先生在北伐之前的北上&nbsp;&nbsp;<br />
　　这是避免战争的最后机会。孙中山先生是否主张北伐？至少他主张召开的国民会议仍然带有民主的色彩。我对此很困惑，也许已经有结论。<br />
<br />
　　上述问题均有人研究过。但是，不少研究要么是围绕已有的结论，要么立足于写翻案文章。我的主张是：细节与真相。<br />
<br />
　　十一、关于辛亥革命的一些反思<br />
<br />
　　1. 对于民主认识的肤浅&nbsp;&nbsp;<br />
　　为了让国民接受民主，革命党人在启蒙的时候把社会问题全部推给专制，认为一旦民主，一切问题可以迎刃而解。这样的见解有两个负面效果：第一，重视革命口号，忽视实现民主的细节问题；第二，当民众看到民主没有带来尧舜，反而是激烈的争斗时，失望油然而生。<br />
<br />
　　2.正如东篱先生所说，革命党人在政治上的幼稚&nbsp;&nbsp;<br />
　　革命胜利以后，出现过“革命军起，革命党消”的说法。至于是否让位于袁世凯，本来只是权衡利弊的问题。对此，革命党内部有人认为，不让位就是有私心。其实，民主并不排斥党派的存在，甚至不排斥党派的利益。关键的问题在于建立民主制度之下的力量平衡，让党派利益不能超越国民和国家的利益。革命党人在初期退让，被排斥之后又采取军事手段，最后终于成为权力的垄断者。<br />
<br />
　　3.汉民族本身的问题<br />
　　推翻民族压迫，建立多数人的统治，是辛亥革命的重要目标。对于大多数国民来说，甚至是主要的目标。应该说，这个目标没有很好地完成。首先是汉人长时间没有掌握决策的权力，缺乏统治的经验，因此在革命之后的国家治理存在不少问题；更重要的是，结束民族压迫的目的本应该是给全体国民以权利，而在事实上早期民国没有做到这一点。在一些给人以深刻印象的事例中，国人甚至感到汉人统治者对待自己的同胞与满清并无二致。正如鲁迅所说：“从前的经验，是从皇帝脚底下学得；现在与将来的经验，是从皇帝的奴才的脚底下学得。”<br />
<br />
　　4.为早期民国辩护<br />
<br />
　　从1912年到1927年为早期的中华民国。在极其艰难的条件下，民国确立了共和政体，还曾经成功地组织了选举。早期民国言论上的自由，思想上的活跃和教育上的独立，都已经被后世所认可。民国的国会是负责任的国会，曾经试图抵制袁世凯和段祺瑞的专权，至少要强于后来的国民大会。在乱世当中，能够如此已经是难能可贵了。当然，也不能回避曹锟贿选，这一事件彻底摧毁了国人对于国会的信任。<br />
<br />
　　早期民国的混乱，国会中的争吵以及军人干政并不能否定共和的方向。这些现象在很多新生的共和国都出现过，坚持下去，不断改进，民主宪政就实现了。至于用“国会议员进八大胡同”来否定民国，是一种片面观点。共和国不是由圣人组成的，主张进步的人不应该以这样的理由废除国会政治。<br />
<br />
　　至于鲁迅所讽刺的军阀一会儿刀枪相见，一会儿握手言欢，一会儿又打起来的现象，除了是丑态之外，还说明那时的政治还讲究留有余地，讲究不为己甚。而后来认定一部分人是敌人，一定要赶尽杀绝，革命彻底了，革命也违背了初衷。<br />
<br />
　　国民党废弃了早期民国的框架，不但抛弃了民主，也没有得到国家的安定与发展，是一个沉痛的教训。<br />
<br />
　　5.最重要的教训<br />
<br />
　　回顾早期民国的历史，我们能够得到的最重要的教训是：民主不能当饭吃。巩固民主的途径应该是在宪政的框架内实现有效的统治，确保社会安定，关注民生，维护底层国民的权益。国民不是政治家，不是经济学家，不是历史学家。政客们对民主的游戏乐此不疲，结果却是民不聊生，这样的民主共和就失去了民意的基础。唐太宗说过，水能载舟，也能覆舟。对于民主共和之舟，恐怕也是这样吧。<br />
<br />
　　脆弱的共和面临着严重的社会和经济问题，而伴随着俄国十月革命的影响，下层民众的权益意识开始觉醒。与此同时，由于第一次世界大战和经济危机，西方民主的说服力也大大减弱了。在如此严峻的形势下，掌权的军人不懂得公民权利，新派的政客又不关注工农的权益，再加上遇到了有苏联支持的反对力量，自然就无法支撑下去了。<br />
<br />
　　我在上一节主张，不可以某些政治家道德上的缺陷否定早期民国。同时，政治人物的道德也是重要的。不论辩护还是反思，都是为了在以后做得更好一些。制度是重要的，但在制度建立和稳固之前，良好的道德形象仍然是政治家可以依赖的资源。特别是那些非强人类型的政治家，道德形象甚至是唯一的资源。<br />
<br />
　　在民主制度建立的初期，制度的力量十分薄弱。与此相反军队不仅是最有效的组织，而且是强力的组织。因此，早期的民主或多或少都处在军人的监护之下。令人不解的是，就是在这样的情况之下，有些国家的文人政治家仍然不能约束自己的行为，从而出现了民主――腐败――军人政治――民主的恶性循环。一些拉丁美洲国家、菲律宾和泰国就进入了这样的循环。<br />
<br />
　　6.中国与日本和土耳其的比较<br />
<br />
　　中国的维新与革命是在接触了外部世界之后进行的，参照了西方的模式。因此，考察中国变革的得失，还应当把中国置于世界的背景之中。<br />
<br />
　　与中国先后实现变革的，还有日本和土耳其。中、日、土三国的变革都是在面临列强威胁的情况下进行的，都是为了变法图强。一个多世纪过去了，日本变革的成功已经被公认，土耳其变革的得失还有待评估。在我看来，虽然土耳其还面临不少问题，但已经建立了稳固的民主制度，保留了民族的文化，同时正在融入欧洲。<br />
<br />
　　有一本名为《狼图腾》的畅销书曾经引起过争议。作者赞扬北方游牧民族的狼性，引发了不少的批评。在这里，我不评价狼性的优劣。说起游牧民族的优点，给我印象最深的就是对待敌人的态度。有时，在争斗中感到确实不如敌人的时候，就学习和追随他们。这种做法有些近乎本能，与道义无关，但在变法图强的时候非常有效。从客观上讲，这是一种追求进步的强大动力。曾为游牧民族的土耳其和有游牧民族因子的日本就是这样。<br />
<br />
　　日本和土耳其变革的最大的特点是，承认自己的落后，虚心地向强者学习。由于不相信自己有什么创造性，所以一板一眼地学。国人说，要有选择地学习西方。不错，日本和土耳其在开始变革的时候就是这样。日本宪法参照了欧洲国家中保守的德意志和奥地利模式，土耳其共和国在创建的初期也不能说是民主的。与中国不同的是，这两国对于下了决心要学习的事物认真地学习，并且不轻易地改变。虽然没有全面地西化，但仍然承认西方比自己先进，这就为不断地进步提供了可能。她们在进步的过程中有过曲折和后退，但不反复无常。<br />
<br />
　　有人说，列强欺负中国，所以不能向他们学习。对此，我们不妨看一看土耳其。由于奥斯曼帝国在第一次世界大战中投向同盟国，战争不但造成了帝国的解体，而且土耳其本土也面临着被占领和瓜分的危险。土耳其国父穆斯塔法●凯末尔成功地阻止了列强的侵略，保卫了国家的独立和主权。凯末尔与西方战斗，胜利以后又向西方学习。年轻的土耳其共和国先后废除了苏丹制度和哈里发制度，确立了世俗政治的原则。与此同时，凯末尔还努力加强在奥斯曼帝国时期弱化了的突厥属性。国际化加上民族性，就是在今天也是明智的选择。另外，土耳其革命得到了苏联的支持，受到了苏联的影响，却没有像中国那样以俄为师。<br />
<br />
　　总的来说，面临与中国同样的难险，日本和土耳其抓住了变革的要点，行动上果断和坚韧，并且具有连贯性。因此，她们与近代的中国相比表现出更高的智慧。<br />
<br />
　　近代的中国人在变乱中惊慌失措，总想找到速效的办法，不知道找到正确的道路，坚持下去，假以时日就会得到正果。我们向先生学习，却又不相信先生。我们在遇到挫折的时候惊慌失措，怀疑取到的不是真经；我们又往往浅尝辄止，小有成绩便沾沾自喜，以为马上就可以超过先生。我们好像是有几分天资的学生，不刻苦学习，成绩并不好，却敢于藐视刻苦用功的同学。<br />
<br />
　　国人迷信“迎头赶上”和“学生超过先生”，总想找到捷径，喜欢更换先生。打一个不准确的比喻，这就好比有些人把学习英语当成头等大事，却不愿意下苦功夫，见异思迁，频繁地换教材，换老师，一次又一次地从ABC开始。<br />
<br />
　　因此，反思近代以来的历史，中国人一定要学会认真和坚韧。切记：进步是从坚持中获得的。<br />
<br />
　　7.再谈国民性<br />
<br />
　　学者们常常谈到国民的弱点，集中起来就是没有常性和不团结。对于前者已经有所涉及，现在谈谈后者。<br />
<br />
　　中国人缺少协作精神，不抱团，不团结，这已经成为定论。这中间的原因非常复杂，也许永远也无法弄清楚。我倾向于认为，汉民族是由很多部分逐渐地融合而成的，是一个文化共同体。不仅如此，这种融合不如其他民族那样彻底，还在继续进行之中。究其原因，一是中国国土辽阔，人口众多；二是古代皇帝的权力巨大，随时有可能侵占个人的权益，并且限制了民间社会的作用。因此，国家对于臣民的保护和关照就十分有限，国家的统一与臣民的福祉没有直接的关系。<br />
<br />
　　我在这里冒昧地探讨民族性问题，是想指出在现代化、国际化的进程中，必须树立现代的民族意识和民族国家意识。应该指出，自辛亥革命以来政治人物所推动的各种进步之中，鲜有维护民众权利的内容。只要强调革命、改革、建设和经济发展的目标，最终结果就是要求民众做出牺牲。<br />
<br />
　　我认为，中国最伟大的进步应该是尊重和保护人权，保护国民生存、追求幸福和表达意见的权利，让国民实实在在地感到国家是自己权益的保护者，让国民在遇到困难的时候感觉到国家是可以信赖的。此外，还要兼顾各地区、各部分中国人的利益关系，反对任何歧视。必须树立这样的观念：只要是中国人，什么事情都可以通过协商来解决。<br />
<br />
　　中国有好客的美德。但是，自古以来的好客是以忽视臣民的权益为代价的。现在，必须强调国家首先要对自己的国民负责。在对外开放、吸引外资和全民学习英语的热潮中，一定要避免给予外国人以超国民待遇，避免对于国人事实上的歧视。<br />
<br />
　　回避上述的要求，增强国家和民族的向心力和凝聚力就是一句空话。</div>

							
							
							
							
