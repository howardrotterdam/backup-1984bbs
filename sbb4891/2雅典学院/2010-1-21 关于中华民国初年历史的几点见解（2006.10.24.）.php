													<h2>关于中华民国初年历史的几点见解（2006.10.24.）</h2>
						
						
																											<div id="postmessage_444805" class="t_msgfont">关于中华民国初年历史的几点见解<br />
　　刘松萝<br />
<br />
　　今年是辛亥革命95周年。将近100年过去了，辛亥革命的理想和目标仍然是21世纪中国人的宝贵财富。网友东篱小居先生在10月10日贴出了《对辛亥革命的反思》，提出了宝贵的见解。这里，我也想与东篱和诸位网友做一些探讨。<br />
<br />
　　首先我要说，世纪学堂是一个名副其实的学堂。网友kimcom所写的《思考的人，还是书呆子？这是个问题》启发我写出了《学者论：也谈思考的人，还是书呆子？》，现在又受到了东篱的启发。<br />
<br />
　　一、 辛亥革命的必然性<br />
<br />
　　1895年，中国在甲午战争中惨败。面对空前的社会和民族危机，18省在京举人联名上书，要求变法。1898年6月，光绪皇帝采纳康有为等人的见解，决定变法。同年9月，慈禧太后镇压了变法运动。戊戌变法的失败，固然有革新阵营的失误，但慈禧的守旧和专权无疑是更加重要的原因。慈禧虽然还政于皇帝，却仍然不愿放弃权力。<br />
<br />
　　为报复西方列强支持光绪，慈禧利用义和团攻击外国公使馆和教堂，并且向列强宣战。八国联军入侵北京之后，她与光绪皇帝仓皇逃往西安。<br />
<br />
　　就这样，晚清不仅专制，而且违背祖制，由太后代替皇帝执掌权力，可谓名不正言不顺。面对外国入侵，朝廷的尊严荡然无存。再加上拒绝变革，失去了新派知识分子的支持。不仅如此，虽然现在的精英们缅怀满清王朝的王道乐土，满清的民族压迫性质是无法回避的。满洲贵族力量的衰落，以及西方传入现代民族概念和自由平等思想，造成了汉民族意识的高涨。晚清违背世界潮流，也不符合专制的规范；失去了汉人的民心，又败坏了满洲的规矩。因此，庚子之变之后，满清王朝的灭亡已经是时间的问题。<br />
<br />
　　慈禧返回北京之后，宣布了大规模的改革措施，包括预备立宪。但是，历史的变迁不以一家和一人的意志为转移。改革不是儿戏，急躁固然不可以，拖延也是不行的。等到老佛爷想通了，想明白了，一切都太晚了。正像有人说过的：杀死了谭嗣同等人，再也没有人去上书了。说辛亥革命是必然的，并不是彻底否定慈禧后来的变革。我想说的是，早知如此，何必当初。变革成功，有赖于统治者的威与信。不幸的是，这两者满清已经不再具备。此时满清统治者的对与错已经不重要，重要的是将要被万民所弃。也许这就是天命、国运、历史潮流和人心所向吧。<br />
<br />
　　改革的大门一旦被堵死，革命的大门就要敞开了。<br />
<br />
　　二、 辛亥革命是革命党和立宪派共同推动的<br />
<br />
　　日本曾经是中国革新力量的避难所。在那里，孙中山等革命派与康有为等立宪派进行过一次大辩论。在很长的时间里，史书把康有为等人称为“保皇派”，这是不公平的。康有为派不但是立宪派，而且是先驱者。史书片面地认为革命派取得了胜利，而事实上这次辩论是进步阵营内部的辩论。革命派认为君主立宪不可行，这被后来的事变所证明。立宪派认为一旦革命，就会造成混乱和分裂，引来列强的侵略。他们的预言后来也不幸而言中了。<br />
<br />
　　国内时局的演进多少证明了满清的改革缺乏诚意，或者缺乏紧迫感。责任内阁本来是一项削弱皇权的措施，实施起来却是组织了皇族内阁。一面改革，一面又要满清衰落的情况下排斥汉族官僚，实行贵族专权，可以说是利令智昏。而收回铁路筑路权的决策，更直接导致了不可挽回的后果。<br />
<br />
　　本来朝廷已经批准民间集资成立股份公司兴建粤汉、川汉两条铁路，而后又反悔，在1911年5月9日收回筑路权并准备转让给外商，以换取外国的贷款。一个虚弱的政权行事却如此专横，激化了各种矛盾。湖南民众首先反对筑路权国有化，湖北、广东也随后加入。四川各界行动最晚，却表现出坚韧和坚决。6月17日，立宪派成立了保路同志会，发起罢市、罢课、抗粮、抗捐斗争。和平的抗议随即被镇压，并且酿成了血案，死亡32人。保路运动随即演化成暴动，民军很快地逼近成都。为了平息四川事态，朝廷从湖北调动军队，造成了武昌的空虚。武昌新军中的革命党外围成员乘机起事，这就是10月10日的武昌起义。有讽刺意义的是，调往四川的新军在途中叛变，带队的大臣端方也被杀了。<br />
<br />
　　应该强调辛亥保路运动的重要性。在中国历史上，鲜有一省人民为了共同的利益一致抗争，遇到镇压也不后退。这是国人权利意识觉醒的标志，是立宪运动的成果。由于过去的史书多为孙中山派所撰写，或多或少地埋没了保路运动的功绩，埋没了立宪派为中国的进步所做出的贡献。<br />
<br />
　　武昌起义爆发以后，南方各省纷纷独立，不少立宪派和旧官僚掌握了政权。对于这个问题，我不赞成立宪派和旧官僚“投机革命”和“窃取革命果实”的说法。没有投机力量的转向，革命是不可能成功的。在革命的初期，旧势力和中间势力的加入甚至是革命党求之不得的。<br />
<br />
　　因此，辛亥革命是革命党和立宪派共同推动的。革命党坚定地主张实现共和并且为此做出了牺牲，具有强大的号召力；立宪派在国内能够合法存在，又有上层的人脉和社会影响力，直接促进了社会的变化。需要指出的是，南方立宪派在宣布独立的省份接受了革命党提出的“中华民国”国号，采纳了军政府的组织形式。<br />
<br />
　　三、 辛亥革命是一场真正的人民革命<br />
<br />
　　辛亥革命是一场真正的人民革命或者国民革命。不少以后成为达人的人，在回忆过去的时候提到了当年参加民军的经历。南方各省组织了各种各样的民军，年轻人抱着各种各样的的目的，比如自由民主，抗击列强，恢复汉人的权利，加入了革命的潮流。南北议和成功之后，民军大多解散了。而当时参军的人在回忆中甚至没有提到要去那里，遇到危险怎么办。<br />
<br />
　　国民的革命，自发的革命，不为某一党、一派的革命，这就是辛亥革命。<br />
<br />
　　四、 辛亥革命是一场光荣革命<br />
<br />
　　武昌起义之后曾经出现了血战，随后就是共和的南方与帝制的北方的对峙。在新的时代，国人用谈判和妥协代替了决战。南北议和成功之后，满清皇帝退位，并且享有优待条件。对此，我不同意“革命不彻底”的说法。用文明的方法解决鸿沟一样的分歧，是中华民族的骄傲。对于促成议和的人们，不论是他处于革命阵营还是保守阵营，都值得后世的称赞。<br />
<br />
　　还要指出的是，在最后的关头，朝廷表现出明智的态度。清帝宣布逊位，而逊位诏书不愧为历史文献。<br />
<br />
　　奉<br />
　　旨朕钦奉<br />
　　隆裕太后懿旨，前因民军起事，各省相应，九夏沸腾，生灵涂炭，特命袁世凯遣员，与民军代表，讨论大局，议开国会，公决政体。两月以来，尚无确当办法。南北暌隔，彼此相持，商辍于途，士露于野，徒以国体一日不决，故民生一日不安。今全国人民心理，多倾向共和，南中各省，既倡议于前，北方各将，亦主张于后，人心所向，天命可知，予亦何忍以一姓之尊荣，拂兆民之好恶，是用外观大势，内审舆情，特率皇帝将统治权归诸全国，定为共和立宪国体，近慰海内外厌乱望治之心，远协古圣天下为公之义。袁世凯前经资政院选举，为总理大臣，当兹新旧代谢之际，宜有南北统一之方，即由袁世凯组织临时政府，与军民协商统一办法，总期人民安堵，海内又安，仍合汉、满、蒙、回、藏五族完全领土，为一大中华民国，予与皇帝得以退处宽闲，优游岁月，长受国民之优礼，亲见郅治之告成，岂不懿欤？钦此！<br />
<br />
　　其中“人心所向，天命可知，……特率皇帝将统治权归诸全国，定为共和立宪国体，近慰海内外厌乱望治之心，远协古圣天下为公之义”写得尤其漂亮。我从来不认为诏书仅仅是空话。顺应天命和民意，正是帝王所应该做的。对于诏书中的善意，要给予应有的评价。<br />
<br />
　　中华民国没有在血海之中和焦土之上建立，归功于所有的历史参与者。这是一场光荣革命，光荣属于我们的前辈，属于中华各族人民。<br />
<br />
　　五、关于满汉两族的关系<br />
<br />
　　清帝的逊位促进了满汉两族的和解。随着革命的胜利，汉人放弃了复仇的目标，与满人和睦相处，这是汉民族历史上光荣的一页。满洲民族对于新的国家基本上是认同的，而这一过程最终完成于抗日战争的胜利。<br />
<br />
　　对于抗日战争中溥仪和川岛芳子等人的行为，应该确认为敌对行为而不是卖国行为。与此同时，大多数满人包括贵族不认同伪满洲国，同各民族一起抵抗日本的侵略。殉国的佟麟阁将军和作家老舍，就表现出满汉两族在国家和文化上的认同。<br />
<br />
　　今天，满汉两族已经成为兄弟。因此，不宜渲染仇恨，也不宜颂扬杀戮和压迫。对于历史，应该客观地记述，从中汲取经验教训，并且允许不同民族有自己的表述。<br />
<br />
