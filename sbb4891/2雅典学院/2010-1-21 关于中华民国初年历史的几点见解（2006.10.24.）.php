																											<div id="postmessage_444829" class="t_msgfont">对辛亥革命的反思<br />
东篱小居<br />
<br />
日本明治维新后迅即崛起为世界性大国，虽经二战的沉重打击，在维新百年之后，成为经济总量仅次于美国的强大国家。并且，国民富足，政治敏猪，无论经济上，还是政治上都成为一个现代大国。<br />
<br />
反观我们苦难的祖国，百年沧桑，人民经受了太多的艰苦磨难，改革开放后才步入正常发展轨道，然而敏猪之路尚遥。这里一定有许多深层的原因需要我们挖掘。<br />
<br />
今天是辛亥革命95周年纪念日，本来想写一篇东西，一来纪念这个不平常的日子，二来对辛亥革命加以反思。可惜时间有限，不可能有大段时间写这样的文章，只好把对中国为何没走上宪政之路的一些思索简单罗列出来，以缅怀这个历史事件，也与学友们一起对这场革命进行反思：<br />
<br />
一、清廷扑灭戊戌变法，堵住了和平变革之路；<br />
<br />
二、甲午战争的失败，宣告了洋务运动的失败。洋务运动是中学为体，西学为用这一思想的实践，其时证明不触动制度不能强国富民，宣示中体西用这一思想的破产；<br />
<br />
三、甲午战争的失败，改变了东亚均势，中国彻底为世界诸民族所看不起，同时也完全丧失了外交主动权，成为列强瓜分的对象；其时，中国的任何强国为目的的变革都不再会得到列强的支持，更遑论如斯宾塞帮助日本设计改革蓝图这样的事情；<br />
<br />
四、清末朝廷的衰落，使清廷与民间形成均势，此时正是宪政改革，立宪敏猪之机。但是，两个原因让中国错过了这第二次历史机遇（第一次是戊戌变法）：<br />
<br />
1、中国只有改朝换代的传统，没有妥协协商的前例，因此，任何一方总是寻机压倒对方；均势不可能制度化维持，只能朝向此长彼落，你死我活的方向演变；<br />
<br />
2、清廷是少数民族政权，汉民族的自尊难以容纳一个少数民族立宪皇室，民族矛盾和对专制的反抗纠缠到了一起，成为解不开的死结；<br />
<br />
五、辛亥革命给中国宪政敏猪一次难得的机会，但是，结果却令人大失所望，究其原因如下：<br />
<br />
1、 革命党人并不真正懂得宪政，比如，辛亥革命的口号是，驱逐鞑虏，恢复中华。这里并没有明确立国的方向；三敏猪义虽然提出了较具体的制度性口号，但是，一个革命政权，没有一个详细的纲领，或制度设计必然走向混乱。这里有两个比较：<br />
<br />
a、法国大革命提出“自由、平等、博爱”的口号，但是缺乏制度设计，从而造成百年混乱；<br />
<br />
b、美国则有杰佛逊、亚当斯、富兰克林等一大批杰出政治人物的制度设计和各州协商妥协，因此建立了一个较稳固的敏猪政体。<br />
<br />
值得一提的是英国，英国的敏猪制度缓慢演变了近300年，起于宪政，归于自由敏猪，因此平稳过渡。<br />
基于中间的则是日本明治维新，既有对天皇拥戴传统上的回归，又有斯宾塞帮助下的制度设计，更有麦克阿瑟主导下的敏猪改革，因此制度建立迅速，人工斧凿痕迹明显；<br />
<br />
2、清廷垮台，各地必然拥兵自重，而且中国地域辽阔，情况各异，革命党人没有及时提出联邦或邦联主张，虽然辛亥革命一起，各地纷纷独立，革命党人却受大一统思想禁锢，不能顺势而为，结果造成军阀割据，战乱不断的局面；<br />
<br />
3、&nbsp; &nbsp;孙中山虽然有华盛顿之量，却无华盛顿之智。同样辞去总统一职，华盛顿把美国交到了一个完善的联邦制敏猪政府之手，而孙中山却交给了一个居心叵测，拥兵自重的权客手中。即使当时也可看出，华盛顿是深谋远虑的宽厚之举，孙中山则是不得已而为之的权宜之计；<br />
<br />
六、国民政府的北伐战争虽然以东北易帜为标志取得胜利，但是，建立在军事征服下的胜利，必然较少宪政敏猪，更多专制倾向（这里有个问题，就是美国南北战争，思考中），更何况对各地军阀的让步，造成了地方军阀割据，中央政府专制这样的滑稽局面。这样的政府虽然看上去专横，其实是一个弱势政府；这样的国家更是一个虚弱的国家； <br />
<br />
七、辛亥革命后，政治精英留学地大多选择日本、法国和俄国（十月革命后苏联直接插手），形成或者国家主义，或者激进主义，或者两者混合物的政治理念，而英美的渐进改良和宪政思想受到忽视，这个结果深深地影响了二十世纪后半叶的中国政治走向；<br />
<br />
八、日本和俄国这两个中国强邻对中国事务的干预，甚至武力入侵，阻断了中国社会自生秩序的形成，严重干扰了中国进入现代国家的进程；<br />
<br />
九、后来的事情大家就都清楚了。<br />
<br />
以上是一些不成熟的思考，望引起麦田网友的讨论，以纪念辛亥革命。<br />
