2010年05月13日 | 分类：政治, 评论 | 添加评论<br />
<a href="http://www.letscorp.net/archives/4712?utm_source=feedburner&amp;utm_medium=feed&amp;utm_campaign=Feed:+letscorp/aDmw+" target="_blank">http://www.letscorp.net/archives ... eed:+letscorp/aDmw+</a>(%E9%B2%A4%E9%B1%BC%E5%A5%B6%E5%A5%B6%E3%81%AE%E5%AE%B6)<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;2010年四月十五日，胡耀邦蒙冤谢世二十一周年纪念日，《人民日报》第二版头条，出现中共总理温家宝题为《再回兴义忆耀邦》文章，大肆赞扬前总书记胡耀<br />
邦。温声称八九年四月胡耀邦病发时一直守在胡的身边；“六四事件”后，每年春节都去胡家探望。该文用意深刻地强调胡的“言传身教使我不敢稍有懈怠”，暗含<br />
着一种悲情宣示。温家宝作为国家总理，打破常规，以个人名义对因犯有“严重错误”而被罢免的前领导人，发出百分之百的肯定强音，这在中共历史上十分罕见。<br />
此文一出，在国内网上引发好评如潮。有网友意味深长的留言：“一个信号，一个前奏，一个铺垫。”而极左派则开始大骂该文是“包藏祸心”，是要改旗易<br />
帜。&nbsp;&nbsp;<br />
　　温家宝面对“倒温”暗流&nbsp;&nbsp;<br />
　　有人解读温家宝此时发文忆耀邦为中共高层集体行为，似乎道理<br />
不通。因中南海集体动作不可能借助在党内一直受攻击的温家宝，从个人感情的角度发文章纪念。有人解读此为团派新动作，仍然牵强附会。在本文看来，温家宝避<br />
开政治话题，巧妙寓意，令党内各方都不便干预，打出一个漂亮的擦边球更符合实际。近两年来，温家宝曾三提“民主是人类共同追求的价值观和共同创造的文明成<br />
果”。今年中国两会前后，他又再三提出公民尊严是“首要价值”。温家宝如此用心良苦地接二连三强调人类共同追求的“价值”，再次遭到党内左派与军内鹰派的<br />
质疑，使之又一次处于重重压力的困难时期，“倒温”暗流有再起之势。&nbsp;&nbsp;<br />
　　其实，温家宝首肯普世价值以来，全国人大常委会委员长吴邦国<br />
二○○九年三月九日，向大会做工作报告誓言“两个绝不”；全国政协主席贾庆林也在《求是》杂志撰文拒绝普世民主。之后，中南海接连发起拒绝宪政道路；接着<br />
中共最高意识形态衙门中宣部又推动《六个“为什么”──对几个重大问题的回答》一书的“社会主义核心价值体系”给全民洗脑，再燃“姓资姓社”争论的烽<br />
火。&nbsp;&nbsp;<br />
　　在此背景下，一向被视为中南海意识形态风向标的中共《求是》杂志和多家官媒与党内外左派势力上下呼应，连续发表了不少讨伐<br />
“普世价值”的文章。这些深藏高端背景的文章认为：严峻的国际形势下，如果放弃马克思主义在意识形态领域的指导地位，搞指导思想的多元化，就是自陷困境、<br />
自毁长城。他们声称，主张中国确立“普世价值”为指导思想与国际接轨，就是“对马克思主义指导地位的挑战”。甚至有极左势力攻击温家宝为党内赵紫阳右派势<br />
力的总代表。国内“早报网”就曾发表过中国华东政法大学张雪忠《自由民主与普世价值》的文章。该文称：中国共产党党刊《求是》杂志最近发表了一篇批判普世<br />
价值的理论文章，文中指出“‘民主、自由、平等、人权、博爱、法治’并不是普世价值，宣扬普世价值也不是什么纯粹的学术问题，而是有着鲜明的政治目的。这<br />
显示中国社会关于普世价值的争论已逐步蔓延到中共理论高层。”由此可见，反普世价值内幕，虽无“衣带诏”玄机，却标示中南海最高层斗争激烈的信号。这便是<br />
此次温家宝打破常规，单枪匹马地间接为胡耀邦被罢免鸣不平的政治生态。&nbsp;&nbsp;<br />
　　众所周知，胡耀邦是一九八九“六四”事件中的敏感人物。一<br />
九八七年被指责支持自由化被迫下台，一九八九年胡耀邦逝世成为后来演变成“六四”事件的全国性学潮的直接导火线。胡耀邦虽在党内已部分恢复名誉，却始终是<br />
一个有争议的人物。温家宝此时在《人民日报》刊登这篇擦边球文章所传递的信息，就是要效法胡耀邦，并隐含缅怀赵紫阳的潜在动机。温家宝在正面赞扬胡耀邦的<br />
同时，反证的却是对以“支持自由化”为名迫害胡耀邦事件的控诉。其实当年“反自由化”与今天反普世价值，如出一辙，一脉相承。所以今天体制内的弱势总理温<br />
家宝，尽管回避政治话题，但完全可以借助他对胡耀邦的缅怀，挖掘出文中隐含三哭的真正意义。&nbsp;&nbsp;<br />
　　一哭邓小平一锤定音&nbsp;&nbsp;<br />
　<br />
　当时的开明领导人胡耀邦力主真理标准讨论和平反冤假错案，从思想上和组织上为改革开放扫清了两大障碍。胡耀邦是名副其实的拨乱反正、改革开放初期实际上<br />
的一线总指挥。这个历史事实是无人可以颠覆的。除此之外，胡耀邦的另一大贡献，就是支持引进了以《第三次浪潮》等西方著作为代表的蓝色文明，是站在中共党<br />
魁角度上接受普世价值的最早推动者。这种把西方自由主义思潮请进中国的直接结果，就是涌现出方励之、刘宾雁、王若望等一批自由派知识分子大力宣扬自由、民<br />
主等理念，即邓小平称之谓的“资产阶级自由化”思潮在中国得到了广泛传播，并发挥了极大的影响力。&nbsp;&nbsp;<br />
　　这种思潮，激怒了一心要给中国<br />
“改革开放”套上“四项原则”龙头的邓小平。为此，一九八六年十二月三十日，邓小平约谈胡耀邦、赵紫阳、万里、胡启立、李鹏、何东昌等，杀气腾腾地直指有<br />
个“资产阶级自由化”的“中央保护层”。邓说“上海的王若望等猖狂得很，早就说要开除，为什么一直没有办？上海的群众中传说中央有个保护层，对是否坚持四<br />
项基本原则，是否要反对自由化，也有两种意见……。”（摘自《邓小平文选》第三卷《旗帜鲜明地反对资产阶级自由化》）。对此，胡耀邦当时的态度十分明确：<br />
反对用“反自由化”和“清污”整人、打棍子。时隔一天，也就是一九八七年元月一日，《人民日报》发表元旦社论，公开披露了邓小平的此次谈话。据《邓小平年<br />
谱》记载，一九八七年一月四日上午，邓小平在家中召集背着胡耀邦的中央政治局常委会议，决定罢黜胡耀邦。于是，温家宝赞扬的前总书记，就这样悲剧性地谢幕<br />
了。此为温家宝为胡耀邦一哭。&nbsp;&nbsp;<br />
　　二哭薄一波落井下石&nbsp;&nbsp;<br />
　　胡耀邦本是薄一波复出的恩人。然而，一<br />
九八七年一月十六日，在中南海里，曾由胡耀邦鼎力主持平反、解放的薄一波，竟又主持了对胡耀邦进行历史性批判、围攻的“生活会”。史称“生活会之变”。会<br />
上，薄一波指诋胡耀邦“反对资产阶级自由化”不力，违反党的四项原则，在重大政治问题上失误。于是，在这次会议上胡耀邦被迫辞去中共总书记的职务。当时，<br />
他忍不住坐在会议室外台阶上嚎啕大哭。这个重要的环节，正是两年后，启动“八九学运”序幕的引子。&nbsp;&nbsp;<br />
　　与此同时，中共中央下达（一九<br />
八七年）一号文件，号召全党全国坚决反对“资产阶级自由化”。第二天，全国各大媒体报刊，都刊载了“胡耀邦正式辞去总书记职务”，接着王若望、方励之、刘<br />
宾雁三人分别于元月十五日、二十日、二十五日被开除党籍，并异乎寻常地昭告全国。至此，在中国的后文革时代，首次掀起将普世价值称之为“资产阶级自由化”<br />
予以批判的舆论高潮，国内政治形势寒流涌动，自由主义思潮遭遇挫折。此为温家宝为胡耀邦二哭。&nbsp;&nbsp;<br />
　　三哭胡锦涛左右徘徊&nbsp;&nbsp;<br />
　<br />
　胡耀邦本也是提携胡锦涛的恩人。一九八○年，胡锦涛被甘肃省前省长李登瀛由省建委副处长提拔为甘肃省建委副主任后，被胡耀邦相中。一九八四年十二月，胡<br />
耀邦前往江西共青城，特意邀胡锦涛同行“考察”后，胡锦涛被任命为共青团中央第一书记，由副厅级升为正部级。这为胡锦涛成为中共最高领导人提供了最关键的<br />
平台。此据亚洲周刊透露，一九九三年四月十五日清晨七点多，时任中共中央副主席的胡锦涛来到江西共青城的耀邦陵园，在墓前肃立良久，突然大声呼喊起来：<br />
“总书记，今天我是来还愿的！”&nbsp;&nbsp;<br />
　　然而，胡锦涛主掌中共船舵后，却并没有追随胡耀邦的足迹，完成他的政改意愿。反而一再宣示中国不<br />
会重复“苏东波”式的民主化改革，也不走普世多党竞争、三权分立的宪政道路。胡锦涛不仅在登基初始膜拜毛泽东的革命理想主义，也崇尚邓小平的模糊战略与实<br />
用主义，政治上左右摇摆，裹足不前。这些年来，政治改革只听雷声有，不见雨下来。更为不幸的是，今天中共全党深入学习实践的“科学发展观”，名义上“以人<br />
为本”，实质上抵制胡耀邦用心引进的蓝色文明与普世价值，全民大唱“党的政策雅格西”。中宣部组织编写的《科学发展观学习读本》一书，宣示“科学发展观”<br />
继毛、邓、江一脉相承，独不见胡耀邦的政治遗产。&nbsp;&nbsp;<br />
　　如今，温家宝在党内势单力薄，施政举措处处受制，迫使他今年两会在答记者会中自<br />
叹“机会不多了”，今后三年要效法屈原“九死未悔”。为公平正义而战的悲情宣示，颇有点当年赵紫阳在天安门看望学生说“我老了”的味道。这难道不是温家宝<br />
撰文纪念胡耀邦悲剧性谢幕的又一哭吗？这一哭也许才是最现实、最生动、最由衷的一哭。&nbsp;&nbsp;<br />
　　暗示深得胡耀邦真传的信息&nbsp;&nbsp;<br />
　<br />
　今天，薄一波的公子薄熙来，又在重庆唱红兴左，与党内外反普世价值潮流遥相呼应。甚至，他近日和其他重庆市领导何事忠、范照兵考察重庆广电集团声称：不<br />
怕人说自己“左”。由此可见，温家宝此时发文高调赞扬因支持“自由化”下台的前总书记，掌掴的不仅是左老薄一波，还有左少薄熙来。如今，温家宝不惜借缅怀<br />
胡耀邦公开党内分歧，要表明的正是深得胡耀邦开明派真传的潜台信息。</div>

							
							
							
							
