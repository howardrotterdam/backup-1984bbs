妥协已到底线　「教宗&#60811;家出声喇」<br />
陈日君：团结推翻爱国会<br />
<br />
 <img src="http://pic1984.12hp.de//up/1269868592.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
陈日君笑言，弱势社群被打压，他会跟他们一起「打仗」动武。黎树雄摄<br />
<br />
【本报讯】刚从罗马回港的天主教香港教区荣休主教陈日君枢机，怒轰由中国天主教主教团及中国天主教爱国会举办的天主教代表大会，指大会企图继续以官方势力控制教会，教会妥协已到底线。陈日君指，教宗已明确指出爱国会有违教会体制，呼吁全体信徒团结一致，推翻爱国会。<br />
记者：张嘉雯<br />
<br />
陈日君昨日出席教会活动时表示，过往不少内地主教在非法祝圣后要求教宗宽恕，教宗顺应意思，亦没有要求相关神职人员离开爱国会，是希望这批神职人员可以从内在改变爱国会，但不代表教廷认同爱国会合法，「我唔要求你大声罾肟爱国会，但唔等于话爱国会，爱国会系唔。」<br />
<br />
「唔可以无条件钔仔」<br />
<br />
<img src="http://imgur.com/1NjQ4.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
陈日君直言，内地主教们不应出席梵蒂冈不认同的天主教活动。图为获教廷认可的北京教区主教李山（左一）。<br />
<br />
陈日君上周初到梵蒂冈出席一连三日的中国问题委员会会议，大会事后发表声明，明确表示内地主教不应参与不合法的圣祭礼仪、主教祝圣及会议，教友应配合，「妥协呢，都有底线 o架嘛。&#60811;家应该系清晰讲真理时候，唔可以无条件钔仔，好紧要 o架，我O要配合呢个政策。以前教宗唔出声，&#60811;家出声喇，爱国会系唔可以接受，大家应该一齐团结，去推翻凇！<br />
<br />
中国天主教主教团及中国天主教爱国会计划今年下半年举行天主教代表大会，选出新一届领导，「呢个大会完全假S，诰烤褂羞阋庖逖剑肯颠硐滴地上教会运作谛枰开呢个会呀，根本唔系，呢个会议唯一意义就系继续用呢个制度去管教会。」<br />
<br />
望国内主教有权领导教会<br />
<br />
为令天主教代表大会更具认受性，内地官方以不同方式威胁神职人员参加会议，陈日君同情内地主教的情况，「不过你系主教吖嘛，系要受呢压力 o架，其实&#60811;家压力都唔系好似以前钕利罚以前抗议就要坐监，就要死 o架罚&#60811;家唔系，&#60811;家主教以温和方式表达，唔会坐监、唔会革职，最多系有人报复，唔畀诶益。」<br />
<br />
他认为神职人员应清晰表达立场，与爱国会划清界线，「如果你唔开始澄清，呢个情况永远落去，系辜负教宗善意。」过去两年内地不少年迈的主教去世，需要安排主教接任，未知会否再出现非法主教祝圣，「新主教大家都赞成，可以同罗马商量，但系你唔可以违反教宗所讲S，教宗都准备同你商量 o架啦。」<br />
<br />
他又指，教廷的新闻稿中特别加入了一句，指培育工作应由主教领导，意思是希望主教的尊严及首要责任受到认同，「希望国内主教们可以真系有权领导教会，唔系俾人领导吖嘛。」<br />
<br />
勇兵之言<br />
送死也要站在弱势的一方<br />
