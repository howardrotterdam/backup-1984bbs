78岁的老人，继续要当基督的勇兵。天主教香港教区荣休主教陈日君枢机昨日出席教区视听中心举办的电影会，与近百教友观赏《战火浮生（ The Mission）》，会后直言若自己是电影中的耶稣会士，他多数会选择打仗，与恶势力对抗到底，即使是「送死」，也要站在弱势的一方。<br />
<br />
「我多数会打」<br />
