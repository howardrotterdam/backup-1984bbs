													<h2>基督教能带给中国民主自由吗？</h2>
						
						
																											<div id="postmessage_827849" class="t_msgfont">以下是我一哥们写的帖子，此人在国内基督新教的教会中混迹多年，我认为此文基本符合我的观察。<br />
我也是基督徒，没有反对基督宗教的意思，曾经也相信基督教能推动中国的民主进程，但是……<br />
<br />
原文标题：基督教离文革还有多远？<br />
<br />
很多经历过文革的人，当他们被邀请接触基督教，接触教会与信徒的时候，都产生一种似曾相识的感觉。 <br />
<br />
如果条分缕析的话，当然我们要说基督教与文革天差地别；但是某些触动人心灵深处的感觉，虽在论证上不易站住脚，却让人能够看到一些被隐蔽的本相。 <br />
<br />
在教会与信徒的虔诚、热情和执着之下，有人看到了文革中的狂热；在教会反智主义浪潮与对思辨的警惕之下，有人看到了洗脑与思想专制的阴影；在对“异端”、“非正统”与“错误”的不宽容之下，有人听见了“大批判”、“大字报”、批斗会的回音；在宗派与宗派之间，教会与教会之间无休止的攻击论断之下，有人感到“造反派”之间文攻武斗的再临…… <br />
<br />
基督教离文革还有多远？ <br />
<br />
或许天启宗教都有极端化的趋向，因为信仰既是至高神的降示，又行诸文字，而成为最高权威之典籍，那么执守乃至拘泥文字就成了一种方便“纯正”信仰的保险做法，而思辨与怀疑则成了危险的游戏，一不小心就会堕入魔道。其实在信仰团体来说，执守某种不可动摇之教条底线原也应当，若无古教会诸信经，基督教会早已一盘散沙。但是设置思想禁区一旦成了内在惯性，最终的走向就很悲哀了。新教运动以来，大体可以看到一个趋势，就是越晚近出现的宗派，其教条越封闭和保守，而其精神则越激烈与偏狭。 <br />
<br />
中国基督教的传播，最有影响力的两次，一是1840年以后国门大开，欧美诸宗派纷纷来华宣教；二是改革开放之后，以国外华人教会为主力的重新传播。如果说第一次尚有改革宗、信义宗、循道宗等诸多宗派带来多元教义，而使宗派合并之后的“三自教会”能有一定超宗派的包容，那么第二波宣教，来华的基本都是带有浸信宗底色的基要派-保守福音派，因为这一宗派最有宣教热情，而令人忧虑的是，这也是最为封闭保守，又激烈偏狭的一派。 <br />
<br />
因此，今日在大陆，如果有人被邀请接触基督教，又不是去了三自教会，那么十有八九接触的是基要派-保守福音派教会（注：近年来灵恩派势头也不小，虽然被基要派-保守福音派指为危险甚至“错误”的倾向，但就其教条与精神来说，其封闭保守和激烈偏狭只在伯仲之间）。 <br />
<br />
教条上的封闭保守与精神上的激烈偏狭相结合，加上中国特有的“诛心”传统，让人看到文革遗风其实是不足为怪的。（中国）基督教距离文革还有多远？其实从骨到皮都让人看到种种迹象。表层来说，基督徒在宣教时早已忘掉了“灵巧如蛇，驯良如鸽”的训诲，用机械的、生硬的“四律”等宣传（propaganda 的宣传而非publication的宣传）材料和同样的宣传口吻，将得救的好消息讲成了杀气腾腾的审判（不是说罪与审判不该讲，但如何来讲是很重要的问题）；再比如，赞美诗的创作（包括词和曲）其实是能够反映时代精神与信仰状况的，国内的一些赞美诗，几乎是向领袖表达愚忠的文革式歌曲的翻版，听起来让人感到肉麻，而失去了圣诗的圣洁、庄重与内在的活泼灵动。内在来说，越来越多教会的教导与信徒的信仰失去了平衡――原本基督信仰中有很多充满张力的平衡，如神的慈爱与公义，预定拣选与人的自由意志，对圣灵的顺服与人的主动性，等等――而变成了一面倒，并由此带来了对强调另一面的教导与信仰实践的论断批判。 <br />
<br />
文革是这样一个年代：愚昧、狂热、缺乏反思、充满战斗激情，以及偶像崇拜。这几点特征，在今日的很多教会中，一个不落。前面几点都比较容易理解，也有一些教会内的批判者（我所见的，如张弓剑弟兄）不同程度地提及了。这里着重说一下偶像崇拜。当前教会内的批判者更多批判的是对牧者的个人崇拜，这是一个问题，但在我看来，尚不及另一种崇拜那么危险，因为对牧者的个人崇拜是个显明了的问题，所有信徒都知道这是个问题；而另一种崇拜，就不仅不被问题化，甚至有成为理所当然的趋势。这就是对《圣经》的偶像崇拜。 <br />
<br />
“唯独圣经”是一个被新教高举的口号。讽刺的是，当年宗教改革中之所以提出“唯独圣经”，为的其实是去掉对人的崇拜，而重申并强化对神的崇拜。可是当这个口号一再被赋予更多含义，当《圣经》变成了真理的化身，当《圣经》被禁绝了错谬的可能，到头来《圣经》取代了神，成了崇拜的对象。 <br />
<br />
必须要指出的是，整本《圣经》是给道成肉身做见证的，是带领人不断深入认识道成肉身的耶稣的，而耶稣才是“道路、真理、生命”。如此看来，真理是活的，正如将基督比喻为“活水”、“活石”。然而越来越多的基督徒在说“《圣经》是真理”，将《圣经》取代了基督，换言之，将道成肉身的见证取代了道成肉身。基督教虽是天启宗教，但与其他天启宗教不同的是，基督信仰的最高“天启”不是文字的降示，而是神降于人间，即为道成肉身。《圣经》是认识真理的管道，但不是唯一管道，在圣礼中，在祷告中，在经历圣灵的恩赐中，都能够与基督相连。 <br />
<br />
而“高举《圣经》”，实际上很容易隔绝人与基督的连结，甚至包括通过《圣经》的连结。《圣经》固然是神所默示的，但既已默示完毕，经文本身就已经是死的了。死物要对活的信仰生活发生影响，只能通过代言人，之于《圣经》就是释经与解经。而当《圣经》被高举，实际上就是释经与解经的意见被高举，当高举《圣经》的人众多时，就有多种彼此矛盾的意见被高举――然而无论多少种意见，都是人的意见，被高举者僭越为神的旨意。从而，高举《圣经》变成了比中世纪更可怕的崇拜人而非崇拜神。 <br />
<br />
这种隐蔽的偶像崇拜，遇到中国这个环境，就变得更加可怕。正如前面所说，如今基要派-保守福音派大占优势，他们对于《圣经》的封闭保守、激烈偏狭的解经，对于很多基督徒和慕道者来说，已经先入为主地成为理所当然。 <br />
<br />
那么在这样的信仰环境下，基督教在中国的“文革化”实在不是杞人忧天。某些基督徒知识分子寄望于基督教在华的广泛传播能够带领中国走出专制传统，走向宪政民主，然而如果基督教在中国无法解“文革”之毒，那么或者就根本无法继续广泛传播，或者一旦广泛传播，会带来不亚于文革的可怕后果，于教会和信徒，于整个社会，都不是幸事。</div>

							
							
							
							
