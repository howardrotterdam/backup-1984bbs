																											<div id="postmessage_828986" class="t_msgfont">基督教在世上的传播，应该主要有四个阶段，有四个重要的关键点，第一关键点当然是耶稣传教；第二是被罗马奉为国教；第三是宗教改革，第四是认同政教分离和宗教宽容。每个关键点之间就是一个主要阶段，每个阶段有不同于其他阶段的显著特点：<br />
<br />
第一阶段（耶稣传教－－罗马奉为国教）：是初始传播阶段，世俗力量很弱小，耶稣本人都因此成为了第一个殉道者，后面大量的信徒步其后尘。但是精神力量无比强大，所以能够在短短的300多年，迅速征服了从平民到国王的广大民众。<br />
<br />
第二阶段（罗马奉为国教－－宗教改革）：是基督教发展壮大时期，同时也是神性人性最为混淆不清的阶段。神性已经从天国走向了世俗，人们开始毫无保留地接受基督教的基本观念，并把他作为第一准则和真理来教育自己的后代。但是，由于人类知识的局限性和人性的软弱，大量人性的丑恶也借助神的外衣危害人间，最典型的例子就是赎罪券。人类干的种种悖逆神的勾当，都以神的名义实行着。很多教士并不能抵抗世俗的诱惑，同时他们有发现自己手中的圣经有超乎想像的权威，他们乐于用这种权威来满足自己的私欲。<br />
<br />
第三阶段（宗教改革－－政教分离宗教宽容）：是人性觉醒的时代。毕竟人类的善良不可能被丑恶完全摧毁。新教的出现，从理论基础上清除了自政教合一、宗教垄断以来夹杂在天主教中种种世俗的丑恶，为人们能够真正认识上帝扫清了道路。人性不是全恶，上帝造人并非要惩罚人，他赋予了人类能够认识真理和上帝的能力，当然这个能力不是完全的，完全的能力只归于上帝和耶稣。宗教改革的意义就是告诉人们：你们有这个能力，这个能力是上帝赋予你们的，任何人不能剥夺这种权利。宗教改革释放了人类寻找上帝之路的激情，但是也暴露出来了人类自负的弱点。人们知道自己有认识上帝的权利，却忘记了上帝一再教导我们：我们的知识是有限的。他们开始把自己理解的教义当作了唯一正确的途径，同时开始使用上帝并没赋予他们的某些权力来推行自己理解的教义，这种教义之间的纷争是如此的激烈和血腥。最后，人类终于在血与火的过程中领悟到了真正的神性，那就是宗教宽容。<br />
<br />
第四阶段（政教分离宗教宽容－－现在）：是人性、神性各归其位的时代。宗教宽容是人性和神性的准确归位：神性是存在的，至高无上的，他可以被人类感知和归附，但是它永远不会被某个人或者某群人完全掌握。坚定自己的信仰，为别人的信仰留出应有的空间，这是神性和人性的完美结合。如果自己没有信仰，是对神性的淡漠，但是认为唯有自己完全掌握了神的旨意，是人性的自负导致对神性的僭越。</div>

							
							
							
							
