																											<div id="postmessage_698989" class="t_msgfont">从毛左与奴性文化的天下观谈起<br />
自鸣.钟 <br />
<br />
前几天，在我的一个帖子里与一位毛派朋友交流，他有一段话很让我诧异。他是这样说的：<br />
<br />
什么叫天是人民的天.<br />
从古到今到中外..什么时候天是人民的.<br />
那都是口号.<br />
不管是谁.<br />
谁都不会作到这点.<br />
<br />
原对话很长，有兴趣的朋友可以去这里：<br />
<a href="http://club.china.com/data/thread/1011/2713/80/17/4_5.html" target="_blank">http://club.china.com/data/thread/1011/2713/80/17/4_5.html</a><br />
我惊诧这话会从毛派的口中说出<br />
<br />
一直以来，我都以为毛派是有理想的，我都以为他们真的相信毛时代的天是人民的天，而他们的一切努力都是在为人民的天而奋斗。而他们自己也始终相信，他们是代表了贫苦大众的利益。<br />
我原来以为，他们之所以成为毛派，是因为他们不了解民主制度。是因为我们国家一直在说，西方是罪恶的，美帝是邪恶的。是因为我们百年来一直受西方欺压，而这时候出了一个男人一点的毛。他们就选择相信人而不是相信制度。他们认定制度都是人定的，制度会因为人而改变。因为一直以来，我们中华的文化就是这样的。<br />
<br />
很多时候，我都能理解毛派，理解他们认识的局限性。<br />
而今天，这位毛派朋友的话击脆了我以前的想当然，原来他们是一直都知道真相的。那么为什么他们还要抬出毛？抬出人民当家作主的旗号呢？ <br />
原因只能是：他们在玩权谋。他们知道毛在贫苦渔民心目中的地位，（60年的单一宣传，平民大众多数已经鱼化了，这也是我能够理解他们认识局限性的原因），他们知道人们当家作主的愿望，他们了解人民的仇富仇贪仇官。一句话他们就是要利用人们对财富的仇恨，对贪官的仇恨，对均富的渴求。来实现自己的政治目的，以期再造一个毛时代。<br />
<br />
于是他们到处寻找敌人制造敌人，制造仇恨。他们反当权派，他们想当权;他们反富人，他们要财富;他们反民主派，民主是独裁的克星。他们为杀官叫好，为杀富欢呼，他们用汉奸，美奴，美分党来称呼他们最恨的民主派。并 目无法纪的号召同党成立锄奸队来铲除他们所谓的汉奸，美奴。令人发指啊！！！不知在他们心中，法为何物，人权为何物。<br />
为了夺权并维护特权，铲除一切反对的人，制造起全民的仇恨。如果这样的人当权国家会怎样？这种人还有脸号称、代表了贫苦大众的利益。这到底代表了谁的利益？只能是他们野心家自己个人的利益。&nbsp;&nbsp;这是中世纪么？<br />
<br />
他们梦想中国的历史一次次的重演，从陈胜吴广、朱元璋李自成洪秀全 到他们。他们梦想延着这条路一路走下去。而事实上，历史上这条路 的确也是一条成功之路。60年前他们的师傅 毛就成功了。 但是他们今天还想重演，那就不明智了，虽然他们号称他们得到了国内大多数贫苦大众的支持。但是民主派得到的、确是全世界的支持，是人类潮流、历史方向的支持。而他们所谓支持他们的贫苦大众，只要捅破那层窗户纸，还原历史真相，贫苦大众会怎么样？，不用我说。他们会怎样对待天安门上的毛，俄罗斯就有实例。<br />
<br />
所以 我劝他们不要天真了，改邪归正真正的站在人民的一边吧！<br />
<br />
昨天又看到一篇奇文，它里面有这么一段<br />
“袁腾飞如果说自己是自由民主，那么，请首先有点风骨，自动辞去共产党给的职务和工作。作为所谓的知识分子，这句话总应该不会忘记：食君之禄，担君之忧。　　<br />
　若是食君之禄，意图谋君，不是造反，背叛，还是什么？　　<br />
　一帮小人，食君谋君，有什么资格讲什么自由民主！”<br />
（　作者：天煞穆珏&nbsp; &nbsp; 文章发于：乌有之乡　）<br />
<br />
看到这段话，我又发蒙了，我掐掐自己，这是什么年代，是大清王朝吗？食君之禄，担君之忧。这句在文人阶层流传几千年的话，现在又有人把它搬出来了。这不是在骂我党是封建帝王吗？我工作我吃饭，与党何干。难道没有共产党我就不活了吗，可世界上60亿人照样活。<br />
<br />
今天的中国号称新中国，人民是国家的主人，如果真的要说这句话，应该是让当官的对人民说。因为是人民在养活他们。在人民是国家的主人的今天，怎么还有这种人，他们的思维怎么还停留在清国，真是奴性不改，典型的奴才啊！。在他们的心中哪有什么人民的天，哪有什么人民当家作主，一切都是主子的天，主子做主。他们想主子之所想，急主子之所急，一切从主子的利益出发，以成为一个好奴才为荣<br />
<br />
这种人真是李莲英再世啊！中国千年的奴性文化，在今天还真是发扬光大。<br />
<br />
在我们这个国家，的确天从来都不是人民的，人民也从来没有真正当个家做个主，坐江山也从来没有人民的份，而打着为人民翻身而坐上去的那些人他们还是人民吗？朱元璋李自成洪秀全是人民吗？那些江山还没有坐稳，就已经站到了人民的对立面的，他们跟被他们打翻的前朝统治者有什么差别，他们照样谋划的是如何管理人民，如何让人们乖顺不要反抗，如何让人民多劳动多交税。他们成功的背后是千千万万被他们忽悠的白骨，一将功成万古枯，江山的得来从来就是万民的白骨。<br />
<br />
而在奴才的眼中，这一切都是再自然不过的事情，人民从来都只是工具，他们眼中只有主子，世界是主子的世界，主子是万岁。在他们眼中，中华千年的文化，最伟大的就是帝王文化，中国一直应该是个帝王的国家。他们梦想的是回到大清王朝，是可以匍匐在地，三呼万岁的时代<br />
<br />
这种奴， 好像世界上都不多了，而在我们中国却特别繁盛，我不想说他们。没有人格的东西，早就该进历史的垃圾堆了<br />
<br />
为什么在全世界都民主的今天，在我们这里，毛派与奴派还有如此大的市场？<br />
<br />
那就是我们从来就没有共和过，从来也没有民主过，人民也从来没有尝过当主人的滋味，我们一直还在自己的历史中没有走出来</div>

							
							
							
							
