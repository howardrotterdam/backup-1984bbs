													<h2>如果突发革命我们该怎么办？</h2>
						
						
																											<div id="postmessage_59682" class="t_msgfont">维基百科：革命，一种激烈的变革。往往是一个利益集团采取暴力等激烈的方式打击另一个利益集团，从而形成不同的利益格局的系列性变革。革命的进程一般比较迅速，速度相对于演变或者改革快得多。蝴蝶效应在社会学界用来说明：一个坏的微小的机制，如果不加以及时地引导、调节，会给社会带来非常大的危害，戏称为龙卷风或风暴；一个好的微小的机制，只要正确指引，经过一段时间的努力，将会产生轰动效应，或称为“革命”。<br />
<br />
革命一词，政治学上的经常相对概念是“改革”。前者指对现行体制的推翻或取代，而后者则是体制内的稍大规模修改，通常是系统性的计划。<br />
<br />
在中国，“革命”一词最早喻指改朝换代，例如：商朝对于其推翻夏朝取而代之的行为称为“汤武革命”。在近代，该词则始于晚清时期，孙中山从日本的翻译著作取出“革命”二字作为推翻满清政府的口号。<br />
<br />
通常，现代意义上的“革命”，含义等同于西文“revolution” 在前工业时代的传统思想观念里，基于人、社会与自然都是由神所创造，并和谐共存，形成一种宁静的秩序。而如果自由民单方面依赖于旁人，并且失去了“德（virtus）”，即个人利益与共同利益的统一，那么整个社会、单独的团体及个人，都会受到持续存在的corruptio （堕落）的威胁。在这种情况下，就可以回到原点（Machiavelli: Ritorno ai prinicipi），由“乱”重新回归到“治”。实际上我们从现代革命运动一直追溯到法国大革命之始，总是可以看到回归“旧秩序（公正、公平）”这样的要求。“revolution”在今天的新的含义，则是1789年之后产生的观点。<br />
<br />
“Revolution”，在社会学以及日常口语中，通常表示一种极端的、常常带有暴力的、对现存政治和交际关系的社会性变革（颠覆）。它由创新者尽可能秘密组织的团体发动，并且能够得到大多数人的支持。<br />
<br />
如果没有极端的社会变革，只是由一个组织或者一个紧密联系的关系网发动的，且只有相对较小的群众基础，这样的行为，一般称为政变，或者在有军队参与的时候称为“叛乱”。这时“Revolution”就成为一种辩解。“Revolution”这个概念，还被用于没有在很短时间内发生的社会变革，例如全球范围持续千年的“新石器时代的Revolution”，或者从英格兰扩展到整个欧洲大陆的，发生于1750到1850年间的“工业Revolution”，而这个Revolution又成为这段时期各个政治Revolution的前提条件。<br />
<br />
按照“revolution”的本来意思，有另一个汉语词汇“反正”。不过自孙中山使用日制汉语词汇“革命”来指代“revolution”以后，便成为习惯翻译。这里，“革”指改变，“命”指命令，即“天命”。“革xx的命”属于曲解“革命”的原意。<br />
<br />
毛泽东曾说,‘革命不是请客吃饭, 不是作文章, 不是绘画绣花, 不能那样雅致, 那样从容不迫, 文质彬彬, 那温、良、恭、俭、让。革命是暴动, 是一个阶级推翻一个阶级的暴烈的行动。’〖一九二七年三月湖南农民运动考察报告〗<br />
