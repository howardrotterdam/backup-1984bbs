																											<div id="postmessage_618060" class="t_msgfont">网易财经：所以最近财政部打算推出社保税，您是反对的？<br />
<br />
陈志武：对。实际上我以前在不同文章里边也谈到过，中国要增加一个社会保障体系这是毫无疑问的，应该要做的一件事。特别是随着市场化深化发展，那么确确实实，对于有的非常低收入的家庭或者说非常不幸运的个人，政府应该承担提供低保的作用，这样的话让每一个中国人至少能够体面的生活下去、生存下去。但是社会保障体系到底是以什么方式建立起来？谁要为这个负责？这个从表面来说，我知道很多学者会去拿北欧的模式、北欧的经历、拿美国的经历、还有其他国家、其他市场经济、私有经济国家的经济来作为一个参照，然后他们就提出来中国也应该把这些社会保障的各种项目转化成为社会保障税。<br />
<br />
但是我觉得这个问题我们必须很谨慎。因为在中国，不管我们喜欢也好、还是不喜欢也好，中国还是一个以国有经济、国有企业为主体的这样一个经济。当初为什么在50年代要进行国有化运动，要把私人财产交给政府、归公给国家？整个这些安排都是为了减轻老百姓税收的负担。因为原来在中国没有个人所得税，你的工资收入里边每个月不会拿走任何一块钱交给政府。原来大家都知道这么多国有企业、这么多国有土地，这些都是为了不仅仅给政府、为政府开支提供收入和财政的基础，也为社会福利提供经济基础。<br />
<br />
在我们没有把国有资产、国有土地进行私有化分给13亿多中国老百姓之前，这些国有财产、国有资产和土地必须用来作为社会保障的基础，而不能够再利用另外一个名义，从老百姓每个月收入中间再要征收社会保障税。不应该以任何新的名义重新征税，而是由没有被私有化、没有被民有化的分给社会老百姓个人的这些国有资产、国有财产来充实社保基金，而不是通过新的税收、新的税种来为社会保障提供支持。<br />
<br />
网易财经：您还曾经建议把国有企业也分给老百姓，但是政府不仅没有这么做，这几年反倒还出现比较严重的国进民退现象，这个您怎么看？<br />
<br />
陈志武：对这个问题我其实还是比较乐观的。按照我说的，最好在全国31个省市都分别成立公民权益基金，然后在中央成立基金。还有中央事业单位，比如像北大、清华、中国科学院，还有其他不同的大学、事业机构，它们都有自己的企业，这些年它们也有自己的土地，也赚了蛮多的钱，它们这些企业股权现在都是在被不同机构掌握。130家左右国有企业的国有股份、再加上事业单位的企业股份，都应该合并到一个全国公民权益基金里边，然后把全国公民权益基金按照同样多的股份形式分到13亿多中华人民共和国公民手里边，31个省市公民权益基金股份基本上可以按照本省的公民多分，外地公民少分这样原则，来实现这样一个民有化改革，以这种方式真正实现我们一直在说的全民所有制。原来的全民所有制走完一大半，但是还剩下最后一步没有走完。把全民所有制中间所有者给它具体化、细化到每一个中国公民、老百姓身上。通过这些公民权益基金股份，具体登记到每一个公民身份证和名字上边，让中国公民成为这些国有资产、国有财产真正、具体化所有者，以此来完成整个全民所有制体系建立的工作。<br />
<br />
这是一个漫长、比较艰巨的任务。尽管现在有很多管理层、一些顾问和决策层都清楚、都听我讲过、介绍过民有化想法和一些方案的细节，社会上和学界也有很多人知道，但是我觉得今天实际在认识上、在意识形态上还不是100%成熟，但是我相信这是时间的问题。因为最后更多决策层的人，尤其民间社会更多人会意识到，当初国有化建立这么多的国有财产、国有资产和土地最后如果不通过民有化方式，落实到每一个公民身上的话，当初国有化初衷就不太容易被实现。<br />
<br />
所以从这个意义来说，只要我们去做，在时间上耐心一点，我觉得最后民有化改革还是会实现的。<br />
<br />
郎咸平的阴谋论是精神鸦片<br />
<br />
网易财经：现在在社会上阴谋论也很流行，宋鸿兵出版的一本书叫《货币战争》非常畅销，另外郎咸平教授最近也在各地演讲中推广阴谋论，比如他在演讲中说，华尔街金融巨头们连中国青椒肉丝价格都控制了。对这样一些阴谋论您有什么样看法呢？<br />
<br />
陈志武：在中国社会，不管是历史上、还是近代，历来就不绝阴谋论。而且很遗憾的是中国社会历来比较相信迷信和阴谋论，因为在我看来迷信和阴谋论实际上性质是一样的。迷信也好、阴谋论也好，往往是对于我们不能够理解、还不了解的事情，在智力上又比较偷懒、又不想去了解，所以就推出各种各样的阴谋论。<br />
<br />
我到世界各个不同地方，走了全球五大洲，都跑了不同国家。我发现，不管是哪个国家都曾经有过很多鬼的故事、有很多迷信的故事。特别是我发现，比较有意思，不管是在澳洲新西兰，还是是非洲，还是在拉美国家，还是在中国其他地方也好，都有面具、鬼具防鬼的。我想这种防鬼的面具是很有意思的现象，因为它的思路是什么呢？我通过戴上那个很吓人的面具，以这种方式把那个鬼吓回去。戴上很吓人面具用来打鬼、用来把鬼吓走，稍微仔细想一想，你会发现这完全是自欺欺人的一种做法，因为真正的鬼是不是怕这个？没有人去研究过，也不会知道的。但是正因为人怕这种很吓人的面具，所以我们就认为鬼也会怕这种鬼具、这种面具。<br />
<br />
不止迷信是这样子，阴谋论也是这样子。物价上涨、房价的上涨、还有金融交易带来的后果，可能这些东西都会影响到我们每一个人的生活，但是这些东西又是我们没有办法控制的、也没有办法理解的东西，不熟悉的东西，那怎么办呢？如果有人说有人阴谋在背后操纵这些事情的话，我们觉得好象真的是这样子，这样一来马上让我们找到，要把愤怒和情绪往哪个方向、哪一个群体、哪一些国家去发泄。但是客观来讲这种阴谋论尽管在智力上给我们带来很多便利，我们不用做更多的深入思考，然后觉得好象是这样子，我把这个叫做智力上懒惰的表现。<br />
<br />
正因为阴谋论听起来很有意思，也和我们情感和情绪非常一致，所以很容易接受这些对象。但是稍微仔细想一想我们会发现，这种阴谋论也好、迷信也好实际上不解决任何问题。因为把华尔街说成是阴谋，把粮价和其他日常用品价格也看成一场阴谋的话，到最后并没有帮助我们真正解决问题。换一句话说，你如果觉得华尔街这么重要，对我们生活影响这么大，带来金融危机可以如此冲击我们的生活，对我们收入带来这么大影响，为什么我们不多去思考、多学习了解金融到底是么回事？了解金融背后的逻辑，有哪些方式也是我们可以学的？<br />
<br />
今天我们发现，中国跨国公司、整个中国决策层很渴望有更多关系到海外投资、海外经营这些方面的知识和经验，但是我们以前把所有注意力――教科书也好、其他书籍也好，都花在帝国主义是如何剥削、如何侵略其他国家、侵略我们中国的，那些资本主义跨国公司怎么压迫我们的。都是这种阴谋论、剥削论和批判、指责的角度，充斥我们所有教科书、所有文章和论文。没有几个真正从建设性角度去看看当年这些跨国公司来到中国和其他国家，他们面对局面为什么是那样？那边局面是什么？他们面对财产权、生命权和商业利益挑战的时候，是如何应对这些问题、应对这些挑战的？因为原来教科书和一般书都没有样建设性角度学习、了解海外贸易、海外投资逻辑，所以以前错失了很多学习的机会，以至于到中国公司今天走出去的时候，我们才发现原来这方面的知识储备在整个中文世界里边是非常稀缺、非常欠缺的。所以今天要补这堂课迫切性非常强。今天，尽管我知道很多人一看阴谋论的书都是很过瘾，因为很情绪化，让你觉得很解渴，但是很遗憾这是知识鸦片，让你感觉良好，情绪上很满足的同时，实际上让你麻醉起来，没有真正学到一些有用、帮助中国企业、帮助中国社会、经济能够更进一步健康发展这些方面的知识和技能。<br />
<br />
经济学是毕生的兴趣和事业<br />
<br />
网易财经：从过去几年来看，您作为学者提出很多建议，但是这些建议基本上都没有被采纳，但您还是一直很热情地提建议，是什么在支撑着您这种热情呢？<br />
<br />
陈志武：那是我自己的兴趣。因为像我这样人，我历来不以自己说什么、提什么建议被哪个政府、哪个国家采用还是不采用，我绝对不是以这种标准来定义我的价值。我写的文章、写的书到底最后被社会、被政府采用还是不采用，喜欢还是不喜欢，我不以这个决定我一辈子的事业、所做的事是成功还是不成功。对于我来说最大驱动力是我自己喜欢。我自己觉得做这些研究、做这些思考，首先是帮助我自己更好认识世界、更好认识一个社会如何组织、如何安排、这个制度秩序应该怎么样，才可以使社会更加良性的发展、更加健康有尊严的发展。我自己这方面知识增加。对于人类社会、对过去的历史、今天的现状和未来的走向，在认知方面，在任何一天能够有所上进的话，对我来说就是我所追求的。<br />
<br />
我觉得对于我自己、还有对于很多年轻学者来说，为了你自己的幸福，最好不要以别人采不采用你的建议、喜欢不喜欢你的理论，不要以这个定义你的幸福感和定义你一辈子价值，而是以你自己到底喜欢什么、什么东西最让你开心、最让你能够有成就感、有满足感，就以这个为准，而不是以别人怎么看来决定。换句话说我活着不是为别人活着，而是为了我自己活着。<br />
<br />
网易财经：您出身农家，同时又在美国接受经济学方面的教育，这给您什么样的独特的认识世界优势？<br />
<br />
陈志武：可能在农村长大，更多对人性这方面有很多自然观察的机会。因为在农村，特别是我，我们一家有6兄弟，我有4个哥哥、1个弟弟，父母也顾不过来，所以我小的时候基本上不受到太多父母的管制和约束，可以让我自己按照我的偏好跟朋友和其他村里边人一起去玩、一起去做事，然后自己体验生活。所以这种比较宽松、不受制约的背景可能对于我自然、自由的发展奠定了一个很重要的基础。<br />
<br />
这些生活成长的经历，包括对于市场的观察为我后来研究和看待社会，起了非常重要、基础性的作用。我在中国一直是读完硕士研究生，1986年去美国，这就意味着我在整个从幼儿到小学、到中学、到大学、到硕士研究生整个价值体系和人生体验和观察，受中国文化和中国意识形态影响比较多。换句话说我对中国方方面面的观念和视角比较了解，然后1986年去了美国以后，又开始接触经济学、金融和美国社会的方方面面视角，又熟悉中国视角和价值体系，同时经过过去 20多年在美国生活和工作，又对美国价值体系有更深、更具体的了解，所以两者放在一起的话，可能更容易让我有机会和视角，一方面对于中国传统的观念和习以为常的这些理念或者理论，做很多的反思。所以从这个意义来说，我过去这些年写的很多中文文章和书，更多是反映这种反思的结果。<br />
<br />
网易财经：您最钦佩的经济学大家是谁？<br />
<br />
陈志武：弗里德曼和哈耶克是我最钦佩的，当然也包括其他一些经济学家，可能大家不一定那么熟悉的。罗伯特.默顿，他是一个对现代金融经济学贡献非常大的学者，他也得过诺贝尔奖，因为他的研究理论偏金融方面，所以国内不是那么熟悉。<br />
<br />
网易财经：您最欣赏的中国经济学者是谁？<br />
<br />
陈志武：我最欣赏的中国经济学者是周其仁、张维迎，还包括一些上一代的学者，像吴敬琏、茅于轼他们都非常不错，对我影响也非常大。<br />
<br />
网易财经：你最反感经济学家做什么样事情？<br />
<br />
陈志武：就是没有原则。我最反感就是没有自己的原则，然后在效果上、在具体表现上是机会主义的这些学者。当然我知道很多人会说因为我是完全主张市场经济的人，完全反对政府过多干预经济，特别是与民争利，这是我觉得任何一个良性社会都不应该有的。但是尽管这样子，如果有一个经济学家他是主张政府干预、甚至主张计划经济，尽管我们在观念上、认知上会有很大差别，只要他不是为了政治或者为了其他利益，是机会主义者，就没有问题。像郎咸平教授，以前我们个人关系蛮近的，到现在也没有什么矛盾。他以前经常跟我说一句话就是，陈志武你怎么这么认真？我个人觉得坚持自己一些基本的原则，我觉得这是每一个经济学家、特别是希望对公众政策、对公众舆论产生影响、对社会产生影响的学者都应该要有基本的原则。认真、理想主义不一定是个坏事。<br />
<br />
网易财经：经济学者最重要的品质是什么？<br />
<br />
陈志武：最重要品质就是坚守自己的职业准则、职业操守。<br />
<br />
网易财经：最恶劣的品质呢？<br />
<br />
陈志武：最恶劣的品质就是为了政治、为了其他的利益随意出卖自己的职业道德，出卖自己的个人人格。</div>

							
							
							
							
