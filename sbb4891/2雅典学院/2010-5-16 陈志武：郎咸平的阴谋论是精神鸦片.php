													<h2>陈志武：郎咸平的阴谋论是精神鸦片</h2>
						
						
																											<div id="postmessage_618056" class="t_msgfont">网易财经5月讯 网易财经《意见中国－－经济学家访谈录》栏目近日专访了耶鲁大学金融学院终身教授陈志武。陈志武出身中国一个普通的农家，对中国的经济转轨有切身感受。他在美国接受经济学教育，受过严格的经济学训练。他对中国有着很深的感情。他对什么都认真。认真地研究，认真地发表公共言论。他的认真为他赢得了广泛的尊重。他认为，中国的诸多问题，都和违背市场规律有关。以下为访谈实录：<br />
<br />
市场经济是中华民族繁荣之本<br />
<br />
网易财经：陈老师，谢谢您接受网易财经的访谈。<br />
<br />
陈志武：谢谢，很高兴今天能够有这个机会。<br />
<br />
网易财经：陈老师，您今年出版了一本新书《陈志武说中国经济》，在这本书里边可以读到您对中国经济的忧虑，您最大的忧虑是什么？<br />
<br />
陈志武：最大的忧虑肯定是国进民退越来越严重，特别是这次金融危机以后，国家在经济中起到的作用、角色不仅仅没有减少，反而进一步被推到一个全新的高度。这种趋势很显然跟1978年以后改革开放总的趋势背道而驰，所以我最担忧的是不同层面的政府、各个不同行政部门重新接管经济、重新管理经济而不只是远远一般意义上的监督、监管经济。<br />
<br />
网易财经：您怎么看金融危机之后，网友们对市场经济所表现出来的越来越强烈的不信任、甚至比较反对的一些情绪？<br />
<br />
陈志武：这是一个很普遍的人类社会现象，就是一旦有危机或者一些简单的、甚至很严重的波折以后，往往把结论下得过快、过于简单。我们应该要记住1978年中国为什么进行市场化改革、为什么进行改革开放？其中一个很重要的原因就是从50年代开始，中国以非常快的速度在学苏联，学苏联的计划经济――实际不止中国是这样子。二战之后，世界上非常多、甚至可以说大多数国家，也包括一些西欧国家，多多少少受到苏联计划经济的成就、也受到凯恩斯主义的影响。具体来讲，大家都熟悉1930年代西方经济大箫条，因为1929年危机使得美国经济、还有欧洲市场经济国家都进入全面的箫条。1930年代西方阵营、西方资本主义阵营大箫条，另一边是1930年代特别是1940年代、1950年代苏联计划经济的快速增长。当时形成很明显的反差和对比，一方面是苏联计划经济按照10%左右速度快速增长，而另一边是西方自由市场经济大萧条。这种反差不止是让1950年代的中国，也让很多拉美国家、非洲国家、欧洲国家、亚洲其他国家都去学由政府介入经济、由政府接管经济、由政府拥有企业、创办企业然后接管、管制各种各样行业。<br />
<br />
除了中国以外，另外很典型的例子就是印度在二战以后特别是印度1947年独立以后，尽管印度没有像中国那样全面进行国有化运动，但是印度也是采用计划经济的模式，也由政府计划委员会、计划部来掌管整个经济的运行。<br />
<br />
在中国，1978年，到计划经济已经没有办法再做下去的时候，才进行让政府慢慢退出市场、让市场自然供求关系更多决定经济资源的配置、解决社会如何发展（的改革）。我们可以看到，在过去30年，中国经济增长非常快、增长非常多，其中一个最最主要的原因，就是经济自由不断恢复，政府不断从经济领域里边退出来。政府退出市场，市场自然的供求关系来发挥更大的效果，个人和企业重新得到经济选择自由，这些就为过去30年中国经济快速增长提供了一个根本的更好的制度架构。<br />
<br />
所以今天我们不能够因为这次金融危机，就忘记过去30年或者过去100多年,中国经过国有化、政府管制经济不利、失败的教训和过去30年让个人经济自由、企业选择自由重新恢复以后，所带来完全不同的正面的经济结果。我们应该记住这种历史、这种不同时期的经历。不要以今天金融危机给世界经济带来打击，就一下子把经历了至少几百年，或者更长时间所证明的市场经济是更有效的一种安排，就把以往更长久得出的结论否定掉。市场经济不是一种宗教、不是一种迷信，而是跟我们人的本性是更加一致的，因为市场经济最核心的原则是自由选择，市场经济是一种基于自由选择的耦合次序。<br />
<br />
你有你的选择空间、你有你的选择权利，别人不应该、包括政府不应该迫使你做什么、或者怎么样做、做多少等等，政府不应该有这样干预的权力。让大家都有自由的选择权，跟彼此、跟企业自由交易以后，在这个基础之上整个社会我们会看到一种基于自由个人和企业自由选择得到的一个均衡状态，就是我们说的耦合次序。耦合次序不是基于任何权力、尤其不是基于行政权力的压迫所产生的。<br />
<br />
政府干预是高房价的根本原因<br />
<br />
网易财经：房价的问题是现在很多网友非常关注的问题，您看中国现在房价这么高，最主要的原因在哪？这种不断高涨的趋势很危险吗？<br />
<br />
陈志武：最主要的原因，我的理解是，政府是唯一的、可以把农用土地转变成非农用、然后把非农用土地可以转成房地产开发的垄断方。中国房地产市场土地供应权完全由各级政府来垄断、掌握。最后各级政府都有非常强的激励去把土地供应尽量压缩在比较稀缺的水平上，这样一来，使得土地的价格抬高以及房地产供应总是保持比较低的水平，我说的是相对需求来说。由此产生的结果一点不奇怪。<br />
<br />
另外一个很重要的原因，就是中国老百姓有了钱以后，他们投资的选择，投资品种、投资渠道的选择，总是压缩在一个很窄的水平上。因为这些年中国老百姓即使有了钱，也没有太多投资渠道，也不能投资到国外，尽管有一些基金、QDII也出来了，但是投资品种数量有限、品种也非常有限。中国老百姓有这么多钱以后，差不多只有房地产、还有银行存款、还有就是股票市场，就这三种可以供他们选择。<br />
<br />
股市它的波动有风险，特别高，所以大家不一定信得过中国的股市。银行的利率很低。所以选来选去。房地产还是大家觉得最信得过的投资的平台、投资的选择。需求被人为、通过政府政策安排相对来说被抬的很高，但是供给被压缩的很小。<br />
<br />
你要说中国如果所有水果就像以前我们熟悉那样子，如果只有各级政府部门才可以卖，价格他们来决定，然后各级政府在卖水果的时候，又是追求利益最大化的话，又有行政权力的支持，又有垄断供应水果的地位、权力，另外定价也都掌握他们手里边，同时社会对于水果的需求也不断提升，大家可以想象那个水果的价格会往哪个方向走。</div>

							
							
							
							
