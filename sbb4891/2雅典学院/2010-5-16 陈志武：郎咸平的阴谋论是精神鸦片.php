																											<div id="postmessage_618059" class="t_msgfont">网易财经：大陆一些经济学家说房价涨那么高，还有另外一个非常重要的因素就是去年因为政府要刺激经济，发出十万亿的新增信贷，导致房地产行业出现大量的泡沫，作为金融学的教授，您怎么看这种泡沫论？您认为这种泡沫论是一种如实的描述吗？<br />
<br />
陈志武：中国的房地产市场我觉得已经很明显，有很多的泡沫。特别是那些一线城市，像北京、上海甚至于深圳。 2009年经济刺激运动中间，银行的信贷规模那么大，96000亿人民币贷款，是2008年的信贷两倍多。从这个里边我们会看到，好多对于房地产产生的效果。<br />
<br />
第一就是整个社会的流动性，因为那些信贷还有宽松的货币政策，包括利率都那么低，由此使得整个社会流动性增加那么多。流动性增加那么多以后，家庭的钱也多了，投资渠道又那么有限，所以对房产的需求肯定会因为货币供应大量增加。所以这种新增加的对房子需求，肯定是让房价一天涨一个水平，房价会越涨越高不奇怪。<br />
<br />
另外，我知道很多人觉得去年天量信贷其中大概有15%―20%投入了房地产行业，供房地产开发商去做更多房地产项目，我看很多人把那个也看成是，让房地产泡沫被炒的、被吹的越吹越大。实际那个我觉得不太符合市场经济道理的。也许供给房地产开发商土地还是太少，给他们提供信贷支持还不够，所以才使得房地产市场能够得到的供给远远低于需求，尤其在北京。<br />
<br />
我有一个学生，他本来在房地产销售公司工作，去年有一个楼盘他们公司负责去做销售，大概有两三百套房子卖，但是来参加买房子的人就排了很长队，大概有三四千人，结果你可以想象。只有两三百套房子，但是只有三四千人都想要买，绝大多数人是买不到的。后来那个房子卖完了以后，我这个学生他看着大厅里边，很多女的在那里哭。很明显那些来买房子的人并不是没有房子住的，他们开着比较好的宝马或者奔驰等等。这些人没有买到房子就在哭起来了，他觉得这样市场太吓死人了、太可怕了。所以他后来干脆辞掉了，他想改行做别的事，因为他觉得这个行业太可怕了。话说回来，好象最近在通州还是其他地方，出现的情况比去年那个哭的还厉害。<br />
<br />
我觉得任何一种市场，不管是哪个行业，任何一个市场如果做到这个份上，这说明这个问题不是在老百姓，肯定在供给问题还有需求渠道上边都出了问题。很多地方被堵死了，所以才产生这种过量的需求，然后被人为压着过低供给，才产生人为供需不平衡，使得房子价格没有别的路可走，只能越涨越高。当然其中一个很重要的原因就是我觉得管理层、管理部门插手太多。不是政府干预不够，而是政府干预绝对太多，才造成过去，特别是过去七八年，房价不断失控。<br />
<br />
政府越干预房价涨得越高，尽管很多时候是政府想要把房价压下来，但越调控房价涨得越。<br />
<br />
最大的收入差距是政府与民间的收入差距<br />
<br />
网易财经：中国政府投资现在在GDP中所占的比例越来越高，这会导致什么样的后果？<br />
<br />
陈志武：这会使得中国经济对出口、对高污染、高排放行业依赖度会更加离谱地高。由此产生的结果，使得中国我们这一代、或者几代中国人不仅仅把未来几千年、或者几百年中国社会生活环境、资源条件都提前由这一代人透支过多。由此产生的结果，我觉得对中国经济在结构上和长久可持续发展方面都会带来非常多的负面、长久的后患。<br />
<br />
网易财经：您曾经建议中国减税1万亿，但是这几年中国政府不但没有减税，反倒税收收入增加很多。从今年数据来看，一季度全国财政收入是19600多亿，这比去年同期增加了34%，听到这样一个数据您有什么感想？<br />
<br />
陈志武：我觉得是太荒唐了。因为2006年，尤其在2008年的时候，我建议减税，尤其2008年免税一年或者 2009年免税一年，当时我提的一些建议，说实在的我自己知道被接受概率并不是太高，因为在中国根本上缺乏这样民主监督制约的机制，没有真正的正式压力机制，去迫使财政部还有其他相关部门去主动启动减税、免税举措。因为全国人大常委会、财经委员会还有其他专业委员会并没有定期举行公开听证，要求财政部官员、还有其他部门官员、国务院那些官员被问责、被质问。<br />
<br />
为什么在2008年、2009年经济危机顶峰的时候，还是没有减税？比如去年还是按照百分之十几的速度，财政收入往上升。今年第一季度财政收入增加 30%多，所有这些都表明在中国实际上民主制度建设、民主监督机制建设和财政透明，这些不再是一些抽象的概念，而是非常具体涉及到千千万万、13亿老百姓自己口袋里到底可以留下多少钱，到底可以得到多少钱的核心的制度安排。所以在这个时候不管是回过头来看，过去两年不管是有危机还是没有危机，政府拿到这一份，照样每年按照差不多是GDP增长速度三倍的速度往上升。<br />
<br />
不管社会上得到收入是多还是少，政府拿走那一块收入继续，总是在不断以快速上升。这个趋势下，如果不遏制住、不停住的话，不通过全国人大常委会这些专业委员会来形成足够的压力，那么中国老百姓从经济增长中间分享到的好处，并不是像人们理解的那么多，也不是像其他国家情况下应该有的那么多。现在很明显国富民穷局面越来越恶化。这个肯定跟政府征税权力、政府税收不受制约、而是失控的局势关系非常大。<br />
<br />
我知道在中国最近这些年，关于收入不均，还有关于收入差距、收入结构方面抱怨非常多，这都是应该进行一些思考和进行一些质疑。但是我觉得有一个方面是被很多学者和业界、社会人士所忽略的，那就是中国最大的收入差距是政府跟民间社会之间的收入差距，而不是民间社会里边不同私人、不同私人群体的差距。当然我知道北京也好、上海也好，还是在中国农村也好，不同人之间、不同人群之间、不同家庭之间收入差距越来越大，这个肯定是存在的。但是我觉得更大的、更严重的、更加失控的收入差距是在政府和民间社会之间。我觉得很多参与收入差距问题讨论的学者和社会业界人士，首先应该把注意力放在政府跟民间社会收入差距不断恶化这个问题上边。因为这个是中国第一位的、最重要的，对于社会负面打击、影响最大、不断恶化的收入差距局面。<br />
<br />
抑制政府收入不断膨胀的问题，不止涉及到老百姓口袋里边能够留下多少钱的问题，而且同时也涉及到整个中国经济增长模式的问题。</div>

							
							
							
							
