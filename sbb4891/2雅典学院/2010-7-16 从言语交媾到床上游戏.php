如果说相识是男女纯本能的吸引，那么相知就是理性的情感体会了。人们因外在的表象而相识，因内在的情感交流而相知。相识可以是对方一副伟岸的身板，一身奢华的装扮，一张灿烂的笑脸，一句生动的语言，都可以瞬间拉近距离，相互认识。<br />
相知却要复杂得多了，两人的感情在相识的基础上自然会要求知道更多，就目前男女恋爱的标准来看，想知道的无非就是双方的工作、收入、学历、家庭背景，个人喜好、性格特点，道德品质等等这些外在内在林林总总无法避免的东西。<br />
这个阶段的相处基本可以决定男女是否能够确定恋爱关系，成功走入婚姻的殿堂，也往往在这个阶段，很多美好的开始在这阶段戛然而止，很多朦胧优雅的感情撕下美丽的画皮露出狰狞的本质，很多天真烂漫的憧憬化为乌有。<br />
大叔对此看法是剥离相识阶段的朦胧，彼此坦诚相待，真实地表达自己的所思所想，也真实地告诉对方的自己一切需要告知的真实情况，比如财产情况，收入情况，家庭情况，工作情况，以及对未来的生活规划，同时也公开自己的身体状况，最好是能做一次身体检查，让彼此有一个足够深入而透彻的了解，谋事在人成事在天，做足了准备工作，让对方凭借你真实的情况作出正确的选择，继续还是分手，这是个问题，但是无法避免的选择，或许会有一点痛苦，但是起码是对彼此的负责。<br />
爱情很美好，现实很残酷，在爱情和现实生活的压力之下必须做一个选择，可能这些林林总总的条件中，并不是每一项都令人满意，但是他（她）依然选择了你，这说明爱情可以开始了。</div>

							
							
							
							
