																											<div id="postmessage_764484" class="t_msgfont">今天在办公室听老赵说第三次世界大战要爆发了 什么共济会要对华人进行种族大清洗 然后就去百度了 结果刚刚发现是&quot;乌有&quot;里面的东西<br />
<a href="http://www.wyzxsx.com/Article/Class20/201007/168671.html" target="_blank">http://www.wyzxsx.com/Article/Class20/201007/168671.html</a><br />
何新：恐怖惊读伦敦共济会秘密文件<br />
<br />
<br />
思考第三次世界战争<br />
<br />
<br />
21世纪前五十年将是人类历史的一个根本性转折点。<br />
人类当前面临着极其巨大的全球性危机，只是我们中国人还在做着经济将无限增长的美梦。而对于迫在眉睫的众多危机视而不见。但是，任何国家都无法长期自我隔绝，置身世外。战争正在不知不觉中，向中国走进。危机的根源在于全球人口过快增长，当前已经接近地球负载的极限。<br />
新中国建立时，1950年，地球上只有20多亿人。中国改革开放之初，1980年，世界上30多亿人。新世纪之初，2000年60亿人。今年已接近70亿人。目前全球每年仍至少增长1亿人，到2050年前后，地球人口将突破90―100亿人。<br />
空气、水、能源、土地，这是人类赖以生存的四大基本要素。西方未来学的研究表明，地球资源最大承载极限是100亿人之内，地球资源和生态无法承载超过100亿而且始终继续递增的人口。<br />
愚蠢而短见的中国精英阶层并不关注人类前途，对日益逼近的生存危机麻木不仁或茫昧无知，不思应对。人们以为，历史永远是线性的――明天、后天将会是今天的继续。他们不相信也不懂得什么是“突变”或“灾变”。但西方的精英则已预察到人类终极危机的迫近，并且在筹谋并且操作着应对之策。最近有一系列征兆性事态出现：<br />
1、今年初自伦敦共济会流传出来的“盎格鲁撒克逊”计划――伦敦共济会的一个种族主义战争文件。[英文原文见本文【附录】的最后部分]<br />
2、俄罗斯与北约走向结盟――谋求建立跨欧亚大陆的泛军事政治同盟。目标显然是共同遏制东方的中国。<br />
3、希特勒的《我的奋斗》在德国被解禁。这是一部反犹太以及反人类的种族主义雅利安（白色人种）文典。<br />
4、美国的导弹防御系统试验成功，清洁核弹头研制成功。<br />
希特勒在《我的奋斗》中把人类分为文明的创进者、保持者和破坏者三种。他认为，只有雅利安人种是文明的代表者。雅利安的使命是消灭劣等人口、劣等民族、劣等文化、劣等种族。<br />
亨廷顿文明冲突论的要义，是鼓吹由西方列强对人类文明进行重塑。而文明冲突的实质是种族冲突。<br />
回顾第一次世界大战的主题，是第一代工业国为划分世界势力范围而战。<br />
第二次大战基本仍继续一战未完成的主题，但在欧洲战场上还出现了一个重要的新主题，就是发生了人类历史上的第一次种族大清洗――消灭犹太人。<br />
二战后，发生了冷战。中国自由派精英至今尚未读懂“冷战”的真实历史涵义。他们鹦鹉学舌地跟着西方叫――以为冷战只是美苏两大阵营的意识形态之战。殊不知，这只是表象。<br />
冷战的真正实质是不发达国家对发达国家的民族解放战争（中国革命的历史性意义也在于此）。冷战的失败者不仅是苏联阵营，也是所有的不发达国家，包括中国。冷战结束前，中国是不发达国家阵营众望所归的领袖。而在冷战结束后，中国陷入了几乎没有任何盟友和朋友的国际孤立之境。<br />
冷战之所以仅仅只是冷战，没有打成全面热战――是由于美、苏、中三个核大国的核武器保持着可以互相毁灭的恐怖平衡。然而新世纪以来美国弹道导弹防御系统的建立，打破了这种恐怖平衡。单方面的核突击一旦成为可能――那么在未来30年内，必定会爆发新的全球性战争――第三次世界大战。<br />
因此，核战争的危险正在日益上升。核战争已经不再仅仅是一种叫嚣，而是现实的威胁。今年2月在伦敦被知情者透露出来的盎格鲁・撒克逊计划――正是作为西方政治和宗教核心组织的共济会对于第三次世界大战的预谋和策划。这个计划宣布了核战争和生物战争时代即将到来，并且冷酷无情地宣布：对未来的战败者将在种族上进行灭绝性的大清洗。<br />
“盎格鲁・撒克逊计划”主要针对的对象就是有色人种，首先是华人。这个文件是1995年旧金山费尔蒙特会议宗旨的扩展――其目标是要消除地球上50%以上的多余人口――垃圾人类，对之进行清洗或者隔离。<br />
这个文件透露，早在2005年的伦敦共济会秘密会议上，就已经确定了发动国际金融战争的计划。所以2008年爆发的美国金融危机，确实是有预谋的一次国际金融的重新洗牌。<br />
共济会是一个神秘的准宗教组织，是英美两国政治和金融的神秘领导核心。一些西方历史学家认为，这个神秘的共济会也是三百年来一系列世界性历史阴谋事件的策划之源。<br />
这次秘密会议的与会者认为：中国的崛起是西方进行人种清洗的最大障碍。因为中国是有色人种中具有健全工业体系、文化体系、国防体系和核武体系的大国。而且也是唯一有能力以国家体系与雅利安人从经济、政治、文化上进行全面抗衡的有色人种大国。因此解体中国是首要的目标。<br />
为达到种族清除的最终目标，在共济会神秘长老团的领导下，西方已经对中国展开金融战争、生物战争（我一直怀疑，“非典”肺炎疫病的发生并非偶然而是一次对华生物战的测试）。并且也正在准备着发动核战争。<br />
实际上，为掏空中国外汇储备、搞乱中国金融而忙碌的经济学第五纵队，也早已进入中国而在积极活动着。<br />
美国近年对华战略的近期目标是遏制中国，把中国龙困锁在国内。中长期目标则是期待中国内乱，自我爆破和分裂。如果中国将来一旦发生大型内乱，美国将通过联合国进行直接的政治和军事干预。这次美国航母和核潜艇联合舰队的黄海以及日本海陈兵，既是一次全面战争的威慑，也是一次警告性的预演。战争，正实实在在地向中国人逼近！也从而证明，这个盎格鲁・撒克逊计划并非虚张声势的虚言！<br />
美国在谋求分裂中国的同时，近年也在暗中策划挑动中印之战，南海之战，以及中俄之战――让潜在对手打击潜在对手。<br />
而可怕的是，多数中国人对共济会已图谋发动的这场种族战争，至今仍然丝毫麻木不觉懵无所知。中国的精英们依然在醉生梦死。反省近十年来中国外交有三点偏失：<br />
1、唯美是从。2、在国际上寡恩无友。3、丢弃了第三世界而自我孤立。<br />
最根本的问题是，一个大国的外交不应无远见、无目标、无方向，外交不该只是一种国际关系的维持会。<br />
为中华民族的久远生存，中国必须有勇气正视和面对日益紧迫的内外危机。应当摆脱亲美外交的思路，走独立自主的路线。必须加快建立核防御和对抗体系，这是遏制核战争爆发的决定条件。在国际上，中国应当交些穷朋友。中国应当审时度势，制订一套有远见的新亚非拉战略。<br />
中国是一个历史悠久的大国。从历史看，秦始皇统一中国后，多次临海刻石，瞩目于海外。汉武帝以下，隋唐宋元明，中国一直是一个雄踞海陆，有勇气挑战世界和走向世界的伟大民族。所谓中国是一个内陆封闭的黄土文明，这是对中国历史的无知妄论。80年代初始倡此论者是我。那时我对中国历史所知甚肤浅。（按：<br />
80年代初我曾讲中国文化是所谓四合院文化、封闭的长城文化云云。见《中国文化史新论》1986，黑龙江人民出版社出版。此论后来被自由派在作品中广泛剽袭。）<br />
中国未来必须寻求开拓出自己的国际生存空间，亟须继续强化人口控制，并制订自己的一套全球化方针。未来中国要想生存而复兴，就必须通过外交、经济、政治以至军事的全方位手段，谋求向外拓展，在全球范围甚至包括宇宙空间范围――寻求新的资源供给地，开拓而逐步建构全球化的大市场空间网络。<br />
对中华民族来说，未来绝对不是一片光明，而将是非常艰难的！近年来我们已经失去了一些重大的国际发展机会！而展望未来，亡族灭种的危机已并非不可想象。<br />
