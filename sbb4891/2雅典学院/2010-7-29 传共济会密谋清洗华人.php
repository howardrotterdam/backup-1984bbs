Anglo-Saxon Mission:<br />
<br />
盎格鲁・撒克逊计划：共济会与第三次世界大战的策划<br />
<br />
<br />
2010年2月17日<br />
<br />
作者（英国）：比尔（Bill・瑞恩 凯瑞（Kerry）・卡西迪<br />
