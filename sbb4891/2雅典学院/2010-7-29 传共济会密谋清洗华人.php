[文件简介和综述]<br />
我们最近收到了一个共济会内部人员发来的11页资料，作者现场出席了2005年在伦敦举行的高级共济会成员（Senior<br />
Masons）会议。会议上讨论的内容实在令人发指。<br />
伦敦是个飞地（译者注：enclave;<br />
具有不同文化的的I土），它的心脏地Ь拖袷歉鼋鹑诜傻亍Ｓ行┤巳衔伦敦几乎有点像梵蒂冈。它很古老，而且历史悠久。伦敦的意义超过纽约，它才是全世界真正的金融中心。<br />
很多研究者认为伦敦这个城市很有可能也操纵着美国的金融系统。这里的幕后人士，控制着美国联邦储备系统(Federal<br />
Reserve)，以及国际清算银行 (Bank for International<br />
Settlement)，纽约和东京。伦敦是全球金融世界真正的中枢神经。而这个中枢系统的背后掌控人则是作为共济会核心成员的大老们。<br />
据与会知情者的透露，这个名为The<br />
Anglo-Saxon Mission（盎格鲁・撒克逊计划）的主要内容如下：<br />
（1）第三次世界大战不久将要发生，这是已经被计划好的，它将是核武和生物武器的战争。情报人士认为它将在未来的<br />
2012-2014年的时间内开始。<br />
（2）计划是从以色列对伊朗的打击开始。随后中国将被挑衅而报以核武器回应。经过简短的核交火后，将会有一段停火。<br />
世界将陷入恐惧和混乱里――所有细节都被精心设计好了。计划已经在某地准备就绪。<br />
（3）在核停火期间，开始进行有计划的生物武器的秘密投放。目标针对的主要是中国人。（何按：令人想起神秘的“非典”与禽流感。）<br />
“中国将发生感冒”（China will catch a<br />
cold）。生物战将会由中国的东部扩散到西部。社会将被瓦解，基础设施将被严重削弱。<br />
（4）在此之后，一个充分的核交火将被触发――这是“真正的”全面战争，将造成大面积破坏和众多生命死亡。<br />
知情人士告诉我们：这一战争计划的最终目标，是将使地球总人口减少50％―90%。他在会上明确听到了与会者提出的这一数字。<br />
这一可怕的图景已经策划准备多年了。以至前两次世界大战，都可以看作为这最后的（末日）启示计划所进行的准备。<br />
（5）2008年10月美国发生的金融崩溃事件，并非意外，而也是在计划中的。这场危机的引发，就是为了加速全球财政资源的集权化控制而精心策划的。（让精英分子把全球货币集中控制到手中。一些经济间谍也已经渗透到中国，目标是造成金融混乱和尽可能掌控他们的金融系统。）<br />
&nbsp; &nbsp; <br />
但是，这一切仍非最终计划。所有这一切，都是为准备应付即将发生的“地球物理性事件”。计划拟定者认为，跟我们的祖先大约在11500年前经历的相类似的事件就要发生，虽然时间不一定像预期的肯定发生在2012年，但也许是在未来10年里的任何某个时间里。我们知道有些事件将发生而摧毁人类的文明，程度甚至超过了核战的影响。<br />
&nbsp; &nbsp; <br />
人们会问：既然有预知的毁灭性地球环境灾难，那么为什么还要发动第三次世界大战呢？<br />
回答是：真正的目标是必须安排筹建一个“后灾难”世界。<br />
为确保按照控制者意图建立一个“新世界”[注意这个词]<br />
，浩劫发生时极权主义的控制结构必须要到位，用民众请求救援的借口，在正义的名义下实施戒严令。那些被精确挑选过的到时实行军事管制的国家会让“精选”的人存活下来，并在灾后的世界里延续文明，开始下一个<br />
11500年周期的循环。<br />
共济会过去几代人在全球范围内所一直秘密策划的，无非是谁将继承地球文明这一件事。那么谁是继承地球文明的“适当”人选？回答是――白色人种。这也许可以解释为什么这个项目的名称是“盎格鲁・撒克逊使命。”<br />
为此，首先要对人口最多的中国民族进行种族清除。因为，新世界只能是由“我们”继承，而不是“他们”。<br />
关于南美，非洲和亚洲的其他第二和第三世界国家人类的未来命运，这个计划中没有透露。据推测，这些国家将暂时被保护，但他们并无法独立生存。世界将由西方的极权主义军事政府所掌控，只有白种人被设定为世界使命的执行人。<br />
&nbsp; &nbsp;<br />
这是一个如此罪恶，如此种族主义，如此残忍，如此庞大的计划，几乎令人难以置信。但是，这些情节与许多评论员，研究人员和内部揭发者在过去几年里揭露出来的许多事情相符合。它是一幅清晰的画面――解释了为何当今的世界是目前现在这种样子的。这个计划的核心部分是关于种族的优选。这也表明：在第三帝国（德国法西斯之后），第四帝国继续存在着。</div>

							
							
							
							
