当我们看到自然的力量以海啸和飓风的形式发威的时候，有没有想过那是什么样的力量？海啸和飓风有组织有结构吗？当然海啸有浪的结构，飓风有螺旋形的风眼，但是它们的基本组成 - 一滴滴的水和空气 - 这些基本组成之间并没有清晰的结构。那些水滴和空气分子之间会相互作用，比如传递压力，但没有任何一滴水是另外一滴水的上级，没有任何一小部分空气会听命于另外一部分空气。此外，那一滴滴水和空气可以说完全没有任何力量。这样的结构，用经常形容人的话可以说是“一团散沙”。但是，在一定的大环境因素下（比如说引起海啸的地壳振动，引起飓风的气压因素，引起大潮的月球引力等），这样的结构可以产生巨大的力量。<br />
<br />
此外还有蝴蝶效应，（<a href="http://zh.wikipedia.org/wiki/%E8%9D%B4%E8%9D%B6%E6%95%88%E5%BA%94" target="_blank">http://zh.wikipedia.org/wiki/%E8%9D%B4%E8%9D%B6%E6%95%88%E5%BA%94</a>）在我个人的理解就是在一个大的和复杂的系统里面，一些开始的时候微小的变化可以经过一系列的放大，产生很大的结果。冒昧地拿唐福珍的事来说一下，也许在某个时候某个人的一些小事使她形成她的性格，在那个令人心痛的时刻，在点燃自己还是保留生命之间她选择了点燃自己，那个模糊不清的燃烧着的网络图片又令更多人心头发颤。我相信很多事情表面上看去没有什么特别的结果，但都在看不到的地方被一级级放大，最后发挥出力量。也许在多年之后，每年她的祭日会有一片越来越大的烛光的海洋，有民选的领导人发表演说“我们再也不要让这种事情发生”，那个时候才能真正看到这件事情引发的能量。<br />
<br />
还有，我认为在大多数的时候变化是在长时间里面潜在发生的，这个时候表面看上去风平浪静，直到内在的压力或者条件变化达到了一个阈值，才会产生看得见的变化。之前看过一部 discovery 的纪录片讲韩国一座商场的倒塌，其中有很多的因素和个人的决定导致了最后的灾难。在我个人的观察，TG现在陷入了一个循环，他们的每一个动作都令更多的人不快，都更加笨拙，虽然使得他们表面上貌似更加强势。从个人的普通的常识来看，我不认为这样一直下去他们会最终实现他们的理想 - 一个稳定的，坚固无比的高压锅。<br />
<br />
总之，我的理解是，所有的这些事情都有意义。我仍然相信这个社会里面的人都还有基本的良知和常识，渴望自由和公正 - 或者说，至少在真相逐渐显现，讨论逐渐展开的情况下，会有越来越多的人成为这样。我相信在这样的情况下，就像海啸和飓风形成的条件一样，社会会产生越来越大的压力。我相信的未来的变化是，在这压力之下，在这人的变化之后，一个个人不需要组织，不需要服从任何命令，会使这个社会发生变化。这个过程会像橙色革命，会发生在大街上。由于变革发生就像飓风和海啸，他们的枪不会有太大的作用，就像用利刃无法切断风一样。具体地说，人们可以在面对枪口的时候选择离开，但那压力不会改变，而枪口不可能永远保持秩序，况且枪最终也是被拿在人的手里，而人也是一个个因素，可以被影响。<br />
<br />
不过任何对未来做预言都是风险很大的一件事。所以我上面说的与其说是我的“看法”，不如说是我在“做梦”。 &quot;you may say I am a dreamer, but I am not the only one......&quot;<br />
