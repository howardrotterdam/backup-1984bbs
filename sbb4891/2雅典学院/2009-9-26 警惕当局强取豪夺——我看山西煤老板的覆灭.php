													<h2>警惕当局强取豪夺――我看山西煤老板的覆灭</h2>
						
						
																											<div id="postmessage_258944" class="t_msgfont">“不患寡而患不均，不患贫而患不安”，抑制豪强是为政要诀，坐以待毙从来是富人的宿命。据新华网报道，山西煤炭官员日前宣布，煤老板阶层行将退出历史舞台。看起来，产业重组这把“政治上正确”的快刀斩下来，二千二百户煤企很快就将减至百户。我还注意到，新华网没忘了故伎重演，特借一位煤老板的肺腑之言道出遵命重组的好处：报效祖国非但不吃亏，而且还能把企业“做大做强”――五十年代资本家企业主敲锣打鼓主动加入“公私合营”，好像都曾经这么说过。<br />
<br />
&nbsp; &nbsp; 时间紧任务重，还有这么多工作让公仆们忙碌，这该有多么地激动人心！这中间又能凭空产生多少寻租机会，看来除了山西省煤炭厅，恐怕也就只有天知道了！当局的理由，想必是冠冕堂皇和刀切豆腐，因为拎着帽子找脑袋，向来不需要有多高的智商：私挖滥采模漠视劳工，阿附权贵矿难频仍，行贿受贿偷税漏税，斗鸡走狗声色犬马，都是些现成的帽子，必有一款适合“倒煤”的老板。只能说“均输平准”“算缗告缗”之后的官府，与民争利越发地不露痕迹直截了当。<br />
<br />
&nbsp; &nbsp; 当局敢于这么做，除了向来不懂得尊重私有产权，还因为在百姓眼里，灭煤老板代表了先进文化的发展方向，和谐社会的可持续发展和党员先进性的保持，能否一勺烩进产业重组旗下，也未可知。以公共利益代言人名义把环境破坏、党风败坏统统归责于煤老板，虽说照样是“领导害病草民吃药”，但能换来公众廉价的喝彩与掌声，因为公众只知道烧煤，但他们不是煤老板。更何况，党和政府虽然看上了你的煤矿，但没打算“白拿”啊――入股、收购加上补偿，好像都挺公平，那你凭啥不踊跃“被重组”？<br />
<br />
&nbsp; &nbsp; 其实，把人分成不同的小撮各个击破，以无关者的民意和多数人的名义，集中损害特定“撮儿”的合法权益，百年历史进程中已演练过多次了，屡见不鲜也屡试不爽。割据在野时打土豪分田地，上台登基后公私合营，把农民分得的土地“通吃”回来，莫不如此。近年来的若干个案，如对城市出租车行业强行集中，将陕北油田“油老板”们集体剿灭，再到此番山西抡圆了想消灭煤老板，其道一以贯之。“国进民退”的产业重组，不过换了个说法，甚至连说法懒得换。<br />
<br />
&nbsp; &nbsp; “人随王法草随风”，我承认煤老板确实有问题，但更相信问题都能从党和政府身上找到原因，几乎都是当局治理问题和吏治毛病所致。有问题并不可怕，但政府要真管而且要依法管：私挖滥采可以管，你管了吗？偷税漏税不难惩治，除了北京对许志永和公盟，你山西又惩治了谁呢？煤老板为富不仁要“解决”，但他钱多了想投身公益，你是肯给登记呢还是真给免税呢？<br />
<br />
&nbsp; &nbsp; 一言以蔽之，既然号称白道，就别使体面的黑道不屑使的手段。合法授予的经营权和采矿许可，不可用运动方式无端收回，否则便是违法和侵权，就该接受诉讼并尊重司法权威。对人家财富哪怕是“不义之财”，安监总局出身的抚台大人可以“眼红”，也不可生出“巧取”和“豪夺”之念。假如以尊重煤老板有权不接受无条件“重组”为底线，那不论入股、折价补偿都可能是真的。要是没了这条底线，即使你说得比唱的还好听，那也只能是假的。<br />
<br />
&nbsp; &nbsp; 在不受监督的绝对权力面前，所有正当权利的绝对值，都只会约等于零。吃喝嫖赌如此奢靡，国家居然如此富庶，而百姓日子依旧这么艰难，很大程度上是因为政府不讲理，对平民的强制拆迁和把煤老板强制重组，都是如此。假如这次山西当局又心想事成，那以前是倒霉的陕西油老板，这回破财的是山西煤老板，下次败走麦城的，就会轮到河北的铁老板――他们在权力面前，同样都是弱者。<br />
<br />
&nbsp; &nbsp; 谓予不信，那就走着瞧。<br />
<br />
&nbsp; &nbsp; 浦志强 2009年8月30日</div>

							
							
							
							
