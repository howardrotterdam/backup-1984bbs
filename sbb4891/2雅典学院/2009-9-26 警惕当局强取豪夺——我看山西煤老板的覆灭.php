													<h2>山西收编小煤矿 温商500亿民间资本求补偿</h2>
						
						
																											<div id="postmessage_258946" class="t_msgfont">山西收编小煤矿 温商500亿民间资本求补偿<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp;&nbsp; &nbsp;2009-09-24 07:00:19　来源: 浙江在线(杭州)　跟贴 1 条 手机看股票 <br />
<br />
&nbsp; &nbsp; 随着山西省国土资源厅一纸通知，温州人在山西投资的500多座煤矿全部要进入被国有大型煤矿兼并或收购的行列。<br />
<br />
&nbsp; &nbsp; 9月2日，朔州，一位温州老板望着冷冷清清的停产煤场不停地叹气 （潘祝平 摄）<br />
<br />
&nbsp; &nbsp; 浙江在线9月24日讯 温州资本曾被冠以“炒煤团”，但今年随着山西省国土资源厅一纸通知，温州人在山西投资的500多座煤矿全部要进入被国有大型煤矿兼并或收购的行列。这场在煤炭行业首次刮起的“国进民退”资本“战役”，将会给温州资本带来怎样的影响？这批数字巨大的温州资本将会去向何方？三名记者远赴山西，深入矿区采访十多天，探究温州人在山西投资煤矿和此次“国进民退”大整合运动的“台前幕后”。 <br />
<br />
&nbsp; &nbsp; 黄益铫是一位在山西省宁武县拥有3座小煤矿的温州投资者。近来，他一直没在山西，而是去了上海等地散心。黄益铫说，宁武曾经是他寄托人生梦想的地方，如今却成了他的噩梦，他不想再踏上这片伤心地了。 <br />
<br />
&nbsp; &nbsp; 山西，这个曾经让温州民间资本辉煌一时的地方，现在又成了众多像黄益铫这样的投资大佬的伤心之地。目前，大部分在山西投资煤矿的温州老板已经悄然离开山 西，仅留下小部分投资者还在苦苦等待当地政府和国有企业的相关补偿，温商500多座煤矿500多亿元投资，不知道还能得到多少回报。 <br />
<br />
&nbsp; &nbsp; 山西“最后通牒” 压减60%煤矿 <br />
<br />
&nbsp; &nbsp; “没有赔偿条款，没有具体补偿金额，早上我‘被迫’与山西汾西矿业集团签订了一个协议。”9月21日中午，记者得到了远在山西宁武的温州煤矿投 资商李松权的最新消息。他在电话中无奈地告诉记者，在宁武的温州煤老板，基本上都签订了这么一个协议，而当天上午，在政府的最后通牒下，他是最后一个签订了协议的温州人。协议的名称叫《进驻被兼并煤矿协议》，这意味着，在李松权等温州煤矿投资人坚持了一年多时间后，他们将彻底离开曾经拥有的小煤矿。 <br />
<br />
&nbsp; &nbsp; 今年3月25日，山西省国土资源厅出台《关于煤矿企业兼并重组所涉及资源权价处置办法》，山西省境内的小煤矿将由大型国有煤炭企业进行全面的整 合重组，山西省政府提出，到2010年，全省仅保留1000座煤矿，兼并重组整合后的煤炭企业，规模原则上不低于年产300万吨，单井生产规模原则上不低于90万吨。此次对小煤矿的整合力度之大前所未有，被称之为煤炭资本领域的“国进民退”。而当时，山西共有煤矿2598座，这也意味着将压减60%以上。 黄益铫的三座煤矿的年核定产量都是15万吨，全部属于要被并购的小煤矿。 <br />
<br />
&nbsp; &nbsp; 前不久，山西省给当地各级政府提出要求，今年9月20日前，各地必须完成所有小煤矿并购接管工作，在规定时间内没有完成并购任务的，要追究各级 政府主要领导的责任。宁武县境内一共62座小煤矿，其中温州人投资的有17座，此次都要被收编“国有”。为了完成任务，宁武县也同样向温商发出“最后通牒”，希望早日完成与小煤矿主的损失补偿谈判。 <br />
<br />
&nbsp; &nbsp; 此次参与宁武县“收编”小煤矿的国有企业主要是汾西煤矿集团，最近，宁武县政府和汾西煤炭集团的人天天给黄益铫打电话，希望黄益铫与并购的国有企业面对面谈判，早日结束并购工作。可是黄益铫都称自己在外地，没有时间。此次，黄益铫受本报记者之约从上海飞回宁武县。 <br />
<br />
&nbsp; &nbsp; 近日，当地资产评估公司对各家小煤矿的资产评估结果出来：年产15万吨的煤矿资产基本上在4800万元左右。按照山西省的有关政策，此次小煤矿被并购的补偿分三块，资产、已缴纳矿山资源费1700万元的返还和其他损失补偿，加起来每座投资亿元以上的小煤矿能得到5000万元―8000万元的补 偿。“其实小煤矿要被整顿并购都是政府的政策，作为民营小煤矿谁都扛不住，之所以迟迟没有跟政府和来并购的国有企业接触谈判，就是不愿看到寄托着自己梦想 的三座小煤矿，就这样一下子化为泡影，这太让人伤心了”。黄益铫叹了口气说。温州资本在宁武县投资煤矿的日子已经不多了。温州人在宁武县投资的小煤矿一旦被并购，也将意味着温州500亿元民间资本筑起的500多座煤矿可能全军覆没。 <br />
<br />
&nbsp; &nbsp; 汾西煤矿集团参与并购工作的张先生说，从目前国有企业并购小煤矿的底线来看，全部按照资产评估公司的评估来给小煤矿主补偿是不可能的，还要打一定的折扣，另外补偿款一次性付给被兼并的企业也不可能，只能是先付一部分，而后分期付款。 <br />
<br />
&nbsp; &nbsp; 温州资本曾是山西煤矿最著名“民间资本”<br />
<br />
&nbsp; &nbsp; 黄益铫的家乡在温州市平阳县水头镇，该镇几乎每年都要受到洪水的侵扰，再加上这里人多山多地少，为了生计，早先年，许多平阳人到山西下矿井挖煤。上世纪80年代初，黄益铫的岳父跟许多温州人一起来到山西下矿井挖煤。<br />
<br />
&nbsp; &nbsp; 上世纪90年代后，随着国家对煤炭资源的放开，许多温州在山西挖煤的人开始以承包、联营等形式投资煤矿，承包年限一般在几年到几十年不等。1996年，黄益铫岳父和几位朋友一起，筹集了200多万元，在山西承包了一口煤井。但是，当时煤炭的价格不仅非常低，还一路下跌，跌到了每吨仅10元。结果不到两年的时间，黄益铫岳父他们就亏了400多万元，结果不得不转让给了别人。而现在，同样还是黄益铫岳父当年的那口矿井，评估价值已达到了5亿元。<br />
<br />
&nbsp; &nbsp; 2000年后，煤炭的市场开始有所好转，特别是2002年到2004年间，煤炭的价格一路攀升，焦煤每吨上升到近400元，再加上国家的政策环境又比较宽松，煤炭投资又一次被温州人所看好，温州资本也又一次涌入山西煤炭行业。到2002年，温州资本已成为投资山西煤矿最著名的“民间资本”。一个在当时盛传的数据显示，温州资本一度掌控了山西60%的小煤矿，年产煤炭8000万吨，占山西省煤炭总产量的1/5、全国的1/20。<br />
<br />
&nbsp; &nbsp; 随着煤炭投资的不断升温，同时山西省也出台了许多引导民营资本进入煤炭小企业的政策。温州资本又源源不断地流向山西，进入煤矿。据介绍，2005年到2006年两年间，仅平阳县水头镇进入山西投资煤炭的资金就在300亿元以上，在山西估计有500多座温州人投资的小煤矿，总投资在500亿元以上。黄益铫正是在这个时期投资近5亿元，在宁武县收购了3座核定年产15万吨的小煤矿。这样算起来，温商有两代人在山西投资煤炭。<br />
<br />
&nbsp; &nbsp; 黄益铫投资宁武县煤矿后，花了一年多时间办理相关开采证件。去年三四月份，证件办齐全了，原本想可以放开手脚大干一番，可是在奥运会前，山西所有小煤矿临时性停止开采，结果一停到了现在要被并购。前后三座总投资5亿元的煤矿仅仅开采了3个月。<br />
<br />
&nbsp; &nbsp; 最大风险来自政府频繁的政策变化<br />
<br />
&nbsp; &nbsp; 2004年前后，温州资本在山西煤炭行业的投资如火如荼，声名鹊起，并被冠以“炒煤团”，备受舆论关注。<br />
<br />
&nbsp; &nbsp; 据在宁武县投资的温州煤老板介绍，在煤矿投资最辉煌时期，许多煤老板可以说每天都沉浸在数钱的喜悦中，那钱都不知道是怎么赚过来的。随着工业发展和一座座煤电企业的建成，作为紧俏货的煤炭一直不愁卖，经常是煤炭还在矿井里，钱都已经到账了。这些情景让许多温州人冲动不已，总想找机会投资煤矿，现在终于成功投资煤矿了，也体会到了投资煤矿的巨大风险了。<br />
<br />
&nbsp; &nbsp; 投资山西煤矿最大的风险就来自政府频繁的政策变化。在山西省出台《关于煤矿企业兼并重组所涉及资源权价处置办法》后，山西省浙江企业联合会通过认真的调查，给山西省政府递交了一份《关于在兼并重组活动中切实维护浙商煤矿企业合法权益的紧急报告》，《报告》中说，近来在山西投资煤矿的浙商，“总是投入巨资给自己的煤矿配置了各种生产设备和安全措施，使煤矿的生产能力、安全水平都得到了前所未有的提升，从而有力地促进了山西省煤炭行业的发展，但是不少煤矿在刚完成基础建设，准备开工时，却不断遇到地方政府的停产令――最常见的是政策变动，地方政策每年都要发文件，要求提高煤矿门槛，包括提高单井规模、提高煤矿产能、提高安全水平等，而这些政策往往是前一次的建设还没有完成，又要按新的政策去投资建设矿井。加上几年来，山西只要有矿难发生，所有煤矿就必须停产检查，而一旦停下来，何时复工就遥遥无期”。这就是风险，而且这风险是不能预料的。<br />
<br />
&nbsp; &nbsp; 2002年，平阳人林泰在大同市南郊区高山镇投资近2亿元与高山村联营一家煤矿，经营期8年。在投资煤矿的几年时间里，几乎是在当地政策的不断变动中度过的。2005年，南郊区一共有170家小煤窑，当地政府计划经过整顿缩减到58家，后来说只保留42家，不久又说只留35家，再后又说仅保留24家。为了使自己的小煤窑能保留下来，林泰不仅投资增加产能和安全设备投资，先后还给当地政府缴纳了3000万元。再加上其他的费用，将原来几年所赚到的钱全部用了进去，结果等了三年的时间，去年7月份，又一份文件下来说，大同市的小煤矿全部要被强制关闭。<br />
<br />
&nbsp; &nbsp; 黄益铫告诉记者，投资山西煤矿心里总是一惊一乍的，一下子被关闭整顿，一下子临时停产，总是没完没了。在没完没了的停产整顿中，煤矿赚不了钱。黄益铫说，他的山西煤矿投资者圈子里，赚到钱的仅三个人。(文中人物除林泰是化名，其余均是真名。)<br />
<br />
