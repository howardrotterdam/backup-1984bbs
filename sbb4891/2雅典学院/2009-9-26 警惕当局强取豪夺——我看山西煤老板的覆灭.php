																											<div id="postmessage_258978" class="t_msgfont">煤老板们的产权 也应受到尊重<br />
<br />
2009-9-24 南方周末<br />
<br />
 <br />
<br />
&nbsp; &nbsp; 2009年4月，《山西省煤炭产业调整和振兴规划》出台，其核心内容之一就是全力推进山西煤炭产业整合。所谓整合，其实是全面收编民营煤矿的同义词。这将导致山西煤炭产业结构的历史性变化，国有或国有控股煤矿将占据绝对主导地位。<br />
<br />
 <br />
<br />
&nbsp; &nbsp; 山西将无煤老板。但在煤老板谢幕的过程中，对产权的保护与尊重不应一同谢幕。此轮对民营煤矿的估价就有剥夺合法获得的产权之嫌。<br />
<br />
 <br />
<br />
&nbsp; &nbsp; 褫夺煤老板有两个堂皇的理由，一是从道德上矮化煤老板们，二是为了实现中国煤炭产业做大做强的战略目标。<br />
<br />
 <br />
<br />
&nbsp; &nbsp; 煤老板的遭遇没有激发公众的同情，人们对于国有大煤矿与煤老板们之间的产权争夺抱持事不关己的态度，一句“山西不要带血的GDP”就足以站在道德的至高点上，剥夺煤老板的产权。<br />
<br />
 <br />
<br />
&nbsp; &nbsp; 缺乏程序正义的错误手段无法达成崇高的目标，反之亦然。即便我们承认煤老板们并非弱势群体，承认煤老板们并非道德上没有瑕疵，也无法得到煤老板们的产权可以被剥夺的结论，否则，我们完全可以借助这一逻辑，剥夺所有小偷、失节者的财产，最终这股剥夺风潮会席卷整个社会。我们曾经经历过这样的时代，留下法律的一地鸡毛。道德瑕疵受到道德指责，违法有法律处置，行政权力的越俎代疱是割肉补疮。<br />
<br />
 <br />
<br />
&nbsp; &nbsp; 至于做大做强煤炭企业更是无稽之谈，如果重回国有化就能做强中国的煤炭产业，相当于一笔抹杀了中国三十年的市场化改革之路。习以为常的说辞并不具有先天的合理性，山西“到2010年，将关闭上千座小煤矿，同时培育若干超亿吨、5000万吨级特大型煤炭‘巨无霸’”，难道就是中国煤炭成为世界龙头企业的证据？事实上，石化行业的国有巨头们早已做大，但在单位生产率等具体指标上远未做强。9月5号，国资委主任李荣融在2009中国企业500强名单及分析报告发布会上，发出警世恒言：“客观地说，今天评的500强，实际上还是500大，企业不仅要做大，更要做强。强而不大，没有足够的影响力，但不至于垮；大而不强，迟早要垮，而且垮的影响也许会更大。”<br />
<br />
 <br />
<br />
&nbsp; &nbsp; 对产权的剥夺有两个层面，一是全盘剥夺，二是强盗式低价赎买，山西在重组小煤矿的过程中使用的手段属于后者。我们常常关注对产权的显性剥夺，却对低价赎买式的隐性剥夺熟视无睹。<br />
<br />
 <br />
<br />
&nbsp; &nbsp; 按照山西省人民政府办公厅2008年83号文件中规定，在国有煤炭企业收购民营煤矿的过程中，评估价格按照2004年民营煤矿矿主缴纳的采矿权价款的1.5倍乘以煤矿的储量计算，2004年的采矿权缴费标准是：每吨焦煤1.8元，每吨电煤1元，因此，按照1.5倍的评估办法，能够给私人矿主的价格就是每吨焦煤2.7元，每吨电煤1.5元。<br />
<br />
&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp; 山西省的煤矿整合过程中有三无现象。既无产权交易所，由公开市场给产权以准确的定价；也没有平等的交易对手，因为国有大煤矿是在行政权力的护航下控股民营小煤矿；更没有进行估价的中介机构，政府文件出台规定的政府指导价说一不二，谁也没有讨价还价的余地。这种强买强卖式的赎买方式，首先违背了契约原则中最重要的自愿与公平的精神，也违背了国务院国资委的相关资产评估标准，更使中国集多年市场改革之力完成的《物权法》黯然失色。<br />
<br />
 <br />
<br />
&nbsp; &nbsp; 在中国的经济领域中，存在着双重悖论――应该由政府定价的公共产品常常在市场的名义下放开价格，而应该由市场定价的商品却在做大做强、保护资源、保护民众利益等种种名义下实行政府定价，于是乎，市场经济成为行政经济，公共产品成为垄断下的暴利商品。<br />
<br />
 <br />
<br />
&nbsp; &nbsp; 对产权的保护与尊重是市场经济发展的前提，否则，就无法阻止有人借着市场的虎皮谋取非市场的暴利。风能进，雨能进，国王不能进，是产权保护中的美谈。如果有一天山西能够自豪地说，我们拥有对法律的敬畏，即便不乏道德瑕疵的煤老板也能在山西获得法律的完备保护，那么，山西离晋商文化的精神也就不远了。<br />
<br />
 <br />
<br />
&nbsp; &nbsp; 现在的局面当然不敢恭维。想当初，多数煤老板都是应山西各级政府的招商之请，相信山西各级政府保护产权的承诺而去的，沧海桑田，不过数年之间，政府信用竟如此轻若鸿毛，令人感慨。看来，现代社会不仅需要建立个人信用记录，建立政府信用记录更应是当务之急。<br />
<br />
 <br />
<br />
注：在南都周刊上发表煤老板一文，很多人认为我说煤老板是瘟疫，这当然不是事实，文章说得很清楚，官商勾结的煤老板现象是一场瘟疫。<br />
&nbsp; &nbsp; 上篇文章讲的是煤老板所处的制度环境，这篇文章讲的是煤老板的产权保护，上下两篇合成一篇文章，能把观点大致说清。<br />
&nbsp; &nbsp; 今天，有朋友提醒，铅笔经济社上有人对我的煤老板一文表示遗憾，我只能说，本人同感遗憾。<br />
&nbsp; &nbsp; 对于煤老板现象的认识应该是立体的，而不是单向度的，我们在呼吁保护煤老板合法产权的同时，不可忽视在经济现象中存在的工人普遍超底线生存状态。否则，这样的市场经济将是缺乏说服力的，也无法获得多数人的认可。<br />
