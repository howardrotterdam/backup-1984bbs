　　谷歌是正义的吗？这可能是它自己说的而已。事实上，谷歌这些年快速扩张，到处侵权惹事，并不具有其所标榜的道德力量。此次所谓的谷歌“退出”，也不是真正意义上的退出中国，它并未放弃在中国的商业利益。目前，谷歌公司在华投资了5家从事技术研发、软硬件及广告业务的企业，都在运营，如今的谷歌公司只是将新闻、图片、搜索三项业务移到香港，更何况香港也是属于中国，说到底还是想继续在中国挣钱。 即便谷歌一直在宣称自己“永不作恶”，可事实上它并没有统一的善恶标准，更多的却是一种双重标准。<br />
<br />
　　首先，中国政府有关互联网公司或任何媒体公司在华营业的管理制度并不是在谷歌“登陆”之后才存在或是仅仅针对谷歌一家公司。谷歌公司是在明确承诺遵守中国的管理制度的前提下进入中国内地市场的。如果谷歌公司如同其自身宣传的那么“高尚”，那么作为“维护人权与言论自由的卫士”，该公司4年前为什么要在如此“原则性”的问题上向中国政府妥协呢？这些自相矛盾的行为本身就证明了谷歌公司的虚伪、无原则和无信用。<br />
<br />
　　更何况，世界上其他许多国家的法律都要求谷歌采取措施审查搜索结果。比如，法国禁止在网上发布有“新纳粹”的信息；在意大利，禁止儿童色情影片。如果说谷歌能遵守他国的法律，那么，谷歌在中国经营也应像所有在华经营的公司一样遵守中国的法律。<br />
<br />
　　其次，各国制度和文化差异几乎是所有跨国公司进入他国必修的第一课。比如在印度的麦当劳餐馆因尊重印度教不吃牛肉的要求不提供牛肉汉堡。如果按照谷歌公司的逻辑，即便在印度，“巨无霸”也要原样不变地在印度复制才算“正宗”。可是如果真的那样做，不仅产品卖不出去，还可能在当地引起宗教冲突。所以谷歌公司宣扬的所谓“全球统一网络自由标准”是行不通的。每个国家的政府和人民对网络自由的解读和接受程度不同，不可能全部依照美国标准去管理。<br />
<br />
　　事实上，谷歌也从来没有执行过“全球统一标准”。 如果谷歌公司真心实意希望全球人民过上全球统一的，美国标准的生活，包括拥有绝对的网上自由，完全可以从执行全球统一工资标准开始。谷歌公司总部所在的加利福尼亚州的最低工资标准是每小时8美元。那样的话，按照加州标准，谷歌中国的员工以每月工作160小时计，每月最低工资限额应是8704元人民币。而这只是最低工资限额，在美国的谷歌总部基本上是清洁工人的工资标准，技术人员的工资至少应该翻倍。如果谷歌在其他国家没有执行这样的“统一标准”，在其他国家的雇员是否可以上告谷歌公司故意歧视呢？<br />
<br />
