庸质疑的侵略行为。联合国此后还多次通过决议，要求以色列从占领地区撤<br />
出，但他们一直公然违反这些决议。如网友所言，以色列敢这么做，当然是背<br />
后有山姆大叔的支持，而山姆叔之所以要得罪全世界回回，当然是因为国内犹<br />
太人的势力太雄厚。<br />
这就是我眼中的加沙的是与非：以色列入侵是因，蛤蟆死的恐怖活动是<br />
果。不能因为恐怖活动惹人憎恨，就忘记了那是谁首先引出来的（其实以色列<br />
搞恐怖活动远在回回之前，只是话题太长，不想再扯个没完没了）。<br />
这是从道义上看。从功利上看，我个人觉得恐怖活动是两面刃，一方面，<br />
它确如那位网友所说，“报复本能起到了平衡种族内强弱差异的作用”。弱者<br />
不择手段的报复在一定程度上威慑了强者，使得后者不敢把事情做绝。如我在<br />
旧作中说，如果阿拉法特当年不干出那一系列恐怖活动来，只怕联合国也不会<br />
通过那么多的决议要求以色列撤军，而以色列也不会同意让巴勒斯坦建国。又<br />
如另一网友已经指出的那样，杨佳滥杀一气，也能在某种程度上威慑恶警，让<br />
他们以后作恶时有所顾忌。但在另一方面，恐怖活动必将剥夺中立者的同情<br />
心，其实是为渊驱鱼，为丛驱雀，把可能争取到的同情者全赶到对立面去了。<br />
因此，要让恐怖报复起到平衡种族内或种族间的强弱差异的作用，就只能<br />
把恐怖活动当成“药引子”，以此作为逼迫对方尊重自己的利益、同意通过谈<br />
判达成合理解决办法的过渡性手段，决不能把它当成万应灵丹。<br />
阿拉法特最后也悟出了这简单道理，意识到靠恐怖活动绝无希望建国，解<br />
决双方分歧只能通过谈判，于是毅然改弦易辙，通过和谈，换取了以色列从部<br />
分占领区撤出，使得巴勒斯坦建国总算有了点现实基础。可惜这明智的道路遭<br />
到左右夹击，最后蛤蟆死竟然当选上台。那伙烂仔毫无政治智慧，以为可以使<br />
用恐怖手段吓得对方屈服，不分良莠乱杀一气，必然要剥夺全世界的舆论同<br />
情，为和平解决中东问题人为设置障碍。<br />
这就是伊斯兰极端分子与共党分子的高下之别。列宁主义者从来反对个别<br />
的恐怖活动，提倡的是革命暴力亦即集体的恐怖活动。其最辉煌的表演就是越<br />
战。<br />
我已经在旧作中说过了，越共本来是侵略者，可他们却有本事把自己打扮<br />
成受害者，专在人类对弱者的天然同情心上下功夫，最后把全世界的浅薄<br />
liberal 都争取过去，在全世界特别是美国本土发动了声势浩大的反战运动，连<br />
简・方达都跑到越南去，当着全世界摄像机镜头谴责美帝暴行，为小诚抨击我<br />
这深刻liberal 提供了绝佳弹药：）<br />
反观回回，真是蠢到了极点，丝毫不知道共党的统战功夫，专门搬起石头<br />
砸自己的脚。“党的建设，武装斗争，统一战线”等三大法宝缺了两个，第二<br />
个还羊肉没吃到，空惹一身骚。<br />
我印象最深的是，英美入侵伊拉克之后，英国与欧洲反战声浪都非常之<br />
高，随着伊拉克治安恶化，反战运动更是风起云涌，可回回就蠢到在这当口上<br />
先在马德里搞爆炸案，后在伦敦搞爆炸，反倒激起了文明世界的同仇敌忾之<br />
心。<br />
说到底，恐怖主义之所以不得人心，是因为它模糊了战争与和平的界限，<br />
不按规则办事，由此对西方生活方式构成了严重威胁，因此势必要为有识者唾<br />
弃。这就是我为何既能看到杨佳和回回们道义上的正当性，又强烈反对暴徒们<br />
无法无天乱来一气。<br />
总而言之，回回的事业是正义的，但也是愚蠢的，因而是不可能胜利的，<br />
中东只会永远乱下去，至少在我有生之年看不到和平希望。<br />
最后说到加沙这事上来，我个人认为，它值得注意的只有三点：<br />
第一，民选的哈马斯政权误国害民的轻举妄动，强烈提示愚昧的人民一旦<br />
有了民主，就更加水深火热。已故亨廷顿所谓“第三波”完全是胡说八道。<br />
第二，中国官方与往昔迥异的反应，强烈提示中共正在从过去唯恐天下不<br />
乱的革命党，蜕变为唯恐天下大乱的执政党，看出在国际上支持恐怖主义必将<br />
在国内引来效法，因此将在中东事务中抛弃对所谓“美国单边主义”的持续谴<br />
责，表现出更合作的态度。建议爱国市侩们看清风向，和党中央保持政治上的<br />
一致，不要光知道瞎反美，免得惹得黎叔很生气，后果很严重（其实我根本不<br />
知道这是谁的话，但在网上看也看熟了）。<br />
第三，回回把恐怖活动提到了一个崭新的阶段，令人深思集体恐怖主义<br />
（包括民主国家的战争行为与极权国家的常态统治在内）与个人恐怖主义（如<br />
回回人肉炸弹）除了功利效果的天壤之别外，到底还有些什么道义上的区别。<br />
