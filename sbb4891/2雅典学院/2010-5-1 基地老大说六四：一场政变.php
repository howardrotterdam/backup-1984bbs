																											<div id="postmessage_593008" class="t_msgfont">基地老大：<br />
puff 1984 - 楼上的几位，其实已经都承认：看了某片，然后感觉某人无耻。 <br />
符合逻辑的推理至少有2种： 1）某人无耻，某片不错 2）某人不错，某片无耻 <br />
有些同学从未有设想过，或者不愿意承认：某片也可以是无耻滴。 <br />
<br />
形成错觉的原因，一般同学有一种惯性思维：美国人制作的片子，还会有错？ <br />
<br />
卡玛同学，实际更是一个北京的老红卫兵。对红色中国有狂热的偏好。 <br />
<br />
卡玛同学，是《天安门》制片人，持美国护照，青少年时期在红色中国长大，在毛共时代参加过红卫兵，曾组织“外国专家子女”红卫兵串联。 <br />
卡玛的父亲韩丁，“中国人民的老朋友”，中共总理周恩来家的座上客。 <br />
卡玛父辈中还有好几个理想主义者，比如叔叔婶婶，不远万里来到中国，参与共产主义血腥革命，土改，三反五反，反右，人民公社，大跃进。。。等，见过共匪屠杀中国百姓无数，始终不曾忏悔“共产主义理想”在中国大陆制造的大规模灾难。 <br />
总之，卡玛一家，是“吃中国”饭的专业户。0:21<br />
puff 1984 - 楼上网友，暴力的先后顺序搞错啦。 <br />
北京城路不拾遗，小偷罢偷，出租车免费载客，饭店免费送餐。。。这些，你从未听说过的，才是当年5月的事实。 <br />
<br />
那么，土共为何要杜撰“暴力“呢？无非是为屠杀找借口而已。实际借口也不英明。因为，按正常的逻辑，无论如何是不需要出动14个集团军的野战军去对付广场上几千个鸟学生的。 <br />
<br />
六四事件，真实的性质，是土共内部的政变，学生与市民，只是不幸被当作了道具而已。 <br />
<br />
<br />
今天吃饱了，俺就多说几句，关于六四镇压的性质，这里网友都没说到点子上，从性质上说，六四镇压与1976年10月的所谓“一举粉碎四人帮”是一样的：如果你承认中共政权，那就是一次军事政变；如果你不承认中共政权，那就是一次匪帮内斗。老百姓(包括学生，普通士兵）都TMD是被利用了的牺牲品。六四镇压的结果是，中共内顽固派（陈云，王震，李鹏，陈希同等等）得以彻底铲除改革派（胡耀邦，赵紫阳的人马）的势力。胡耀邦是前任总书记，六四运动的导火索就是胡耀邦被赶下台后郁闷地突然病逝，大学生们念其做了不少好事，自发去天安门广场悼念。赵紫阳是现任总书记，同情学生，主张改革。赵，按照中共自己的逻辑，是第1把手；所以通过六四镇压来推翻赵的势力，（软禁赵紫阳一直到死，没给出啥罪名），就是一次不折不扣的军事政变。这与上回叶剑英华国锋他们“逮捕四人帮”是一样的性质。只是时代在进步，中共在六四以后，没把赵紫阳拉出来给按个罪名审判一下，而是悄悄地软禁了他。就如人间蒸发一样。（这也说明中共这个组织的匪帮性质，没什么上得了台面的规矩；某种程度上，连匪帮都不如，盗亦有道，黑帮分子有时比中共先进）。后台总老板，当然是邓小平啦，这老邓比较复杂，既是顽固势力的总舵主，又是改革势力的“总设计师”。但比较遗憾的是，六四这回，老邓选择了做顽固派总舵主，等于亲自把自己扶植起来的改革派势力一网打尽（胡耀邦赵紫阳都是老邓挑选出来的人才）。但老邓是有自知之明的，干了这件遗臭万年的事，知道来日不多，于是指定了江泽民胡锦涛这等平庸派为接班人，而不是向顽固派所梦想的那样把政权交回给中共保守势力。六四对老邓的打击也是致命性的，老家伙不久就不省人事瘫痪成了半个植物人，并嘱咐家人死后烧掉不留骨灰，省得日后被北京人民找出来羞辱一番。 <br />
