																											<div id="postmessage_572718" class="t_msgfont">3.林松先生韵译的《古兰经》<br />
<br />
《古兰经》是先知穆罕默德在不同的地点和场合分段颁布的。此外，先知颁布经典的时候，使用的是押韵的散文。了解了这两点，阅读和理解就会顺利一些。<br />
<br />
在书市上，我看到了民族学院展台上林松先生韵译的《古兰经》。当我有些疑惑的时候，老师和同学们告诉我：《古兰经》的原文就是分段和押韵的散文。一位老师还拿出阿拉伯文和阿文汉文对照的《古兰经》，她说：原文就是这样的。<br />
<br />
林松先生是一位虔诚、博学而且谦和的学者。他有主见又循循善诱，工作有进展又不否定前人。此外，他在注释中尽力解惑，同时又复述经学家的观点：应该以理解经文为主，不可妄加猜测。<br />
<br />
我认为，在学术上，林松先生的所述所为堪称典范：有信仰，有创建，有风度。</div>

							
							
							
							
