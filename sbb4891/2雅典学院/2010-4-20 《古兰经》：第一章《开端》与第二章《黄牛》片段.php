																											<div id="postmessage_572661" class="t_msgfont">1.先知穆罕默与《古兰经》<br />
<br />
先知穆罕默德曾经是一位商人。在经商的旅程中，接触到了犹太教和伊斯兰教。以后，在安拉（神）启示下，先知穆罕默德逐段颁布《古兰经》，并且由后世信徒编纂成书。<br />
<br />
《古兰经》强调安拉的唯一性。而安拉，也就是犹太教中的耶和华，基督教中的神或者上帝。创世，洪水，出埃及等等的故事，在犹太教、基督教和伊斯兰教中都有。尔萨（耶稣）的事迹，在基督教和伊斯兰教中都有。在因此，有学问的人，切不可说出“假如上帝面对真主”之类的话语。<br />
<br />
《古兰经》认为，安拉在先知穆罕默德之前降示的经典，包括《讨拉特》（《旧约》）和《引芝勒》（《新约》）都是有效的。事实上，不读《讨拉特》和《引芝勒》，《古兰经》是看不懂的。《古兰经》把犹太教徒和基督徒称为“有经典的人”，阿文强兄弟在讨论的时候，也说过“有经的人”。按照伊斯兰的教义，犹太教徒和基督徒严格遵守安拉降示的经典，也可以进入天堂。<br />
<br />
在一本佛教读物中，把犹太教、基督教和伊斯兰教都称为“一神教”。<br />
<br />
同时，伊斯兰教比犹太教和伊斯兰教更加严格地遵循一神和反对偶像崇拜的原则。《古兰经》认为，《讨拉特》和《引芝勒》被有经典的人篡改了。比如，安拉是唯一的，不可能有儿子尔萨，尔萨是麦尔嫣（玛利亚）的儿子。在伊斯兰教内，先知穆罕默德也是不可以与安拉并列的。作为穆斯林，自称“安拉的仆人”是一种无上的荣耀。<br />
<br />
塔利班炸毁巴米扬大佛像，是把不准崇拜偶像的教义极端化了。<br />
