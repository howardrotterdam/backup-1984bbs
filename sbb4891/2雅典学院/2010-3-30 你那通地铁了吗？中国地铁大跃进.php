													<h2>你那通地铁了吗？中国地铁大跃进</h2>
						
						
																											<div id="postmessage_539174" class="t_msgfont">全国地铁大跃进，北京上海沈阳南京无锡……，小小的南京，居然规划了17条地铁。<br />
<br />
地铁就是政府操盘整个城市的手段，地铁一响，黄金万两。通地铁的地段，周边地块价格马上翻番！同时，这也是政绩工程，见效最快，问题是我们需要这么多地铁吗？城市里全是洞啊！可怕！<br />
<br />
<a href="http://www.nbd.com.cn/newshtml/20100328/20100328110020240.html" target="_blank">http://www.nbd.com.cn/newshtml/20100328/20100328110020240.html</a><br />
<br />
发改委：中国28个城市建设地铁规划获批<br />
<br />
眼下雄心勃勃的规划，热火朝天地建设地铁的城市可远不止深圳，国家发改委基础产业司正局级巡视员李国勇昨天透露：目前，有33个城市正规划建设地铁，到昨天已经28个城市获得批复。<br />
<br />
　　李国勇：国务院已经正式批准的是25个城市，现在分为两批，头批是15个城市，第二批是10个。现在已经拥有地铁的城市是十个，近期还要批准3个，还有兰州也提出来了，还有一些城市也在准备，十二五期间，我们可能要把规划做好，鼓励大家在优先发展公交的战略前提下，需要轨道交通的，我们继续支持。<br />
<br />
　　按照现有规划，今年我国将建成地铁480公里，2020年总里程将达6100公里。2010至2015年地铁建设投资规划额将达11568亿元，将是今年的5.93倍，是什么激发了33个城市喷薄的地铁建设热情?<br />
<br />
　　专家和各地地铁建设部门的官员一致认为：我国经济快速发展后，机动车激增导致地面交通拥堵，建设地铁显然是缓解地面交通压力的有效方式，中国工程院院士王梦恕教授这样分析：<br />
<br />
　　王梦恕：咱们现在整个国家调整城市的拥挤现象非常严重，现在城市发展不能摊煎饼了，像北京摊到六环了，再往前摊没那么多土地占了，现在要立体城市，要向底下发展，这是自然规律。<br />
<br />
　　国家对基础设施建设的加大投入，也成为推动各地地铁建设热情高涨的原因之一，郑州市轨道交通有限公司总经理郭拥军：地铁工程是一种急需必要得民心工程，这里面一个机遇却是咱们去年面临世界性的金融危机以后，咱们国家靠投资拉动内需，面临着好的发展机遇，在机遇的前提下地铁的建设行程加快了，我看后面咱们今后10年、20年甚至更长时间，地铁会遇到黄金发展期。<br />
<br />
　　郑州即将逐步迈入地铁时代<br />
<br />
　　作为河南省的省会，郑州在这次地铁建设的热潮中也不甘落后，计划分3个建设市内地铁，未来郑州的地铁蓝图是怎样的?市民们对此又作何反应?<br />
<br />
　　按照规划，郑州市轨道交通线网由6条线路组成。2015年前，将建成1号线一期工程和2号线一期工程，全长45公里，形成&quot;十&quot;字形构架的骨架网，郑州即将逐步迈入地铁时代，市民怎么看呢?<br />
<br />
　　市民：郑州已经发展到国内的特大城市之列，所以说，如果你要不建轨道交通，不能充分的提高城市的品位。<br />
<br />
　　地铁带来的是不仅便利，市民郑女士从红透郑州的地铁上看到的是商机：<br />
<br />
　　郑女士：从我们下到楼梯，楼梯间的广告，还有我们在候车的时候，因为很多立柱，不包装是很难看的，但通过简单的包装，它的视觉效果非常非常好，那块的广告效益非常好，列车内的广告，包括报站广告，如果按地铁整个商贸系统来盈利的话，地铁广告可能会占1/3。<br />
<br />
　　地铁改变的，还有人们生活的半径和节奏，&quot;地铁有多远，我们就敢买多远!&quot;成为地铁族们买房时的宣言。<br />
<br />
　　市民：以前比较偏僻的地方，现在如果有地铁站的话，肯定房子卖的比较好，房价也会升。<br />
<br />
　　地铁文化是城市文化的缩影，如何让郑州地铁承载人文之美，市民邓女士提出了她的理解：郑州有3000年的历史文化了，我也希望我们能够有更多的历史文化的元素能够参与在地铁中。比如说在地铁站的一些宣传包括地铁站的一些建造以及我们在地铁中的一些风格，能够更加跟古都的文化结合起来，我们就觉得更好了。<br />
<br />
　　轨道交通建设考验城市规划智慧<br />
<br />
　　郑州地铁建设的历程，可以作为中国城市地铁建设的一个缩影，中国正在加速走进&quot;地铁时代&quot;，目前我国的地铁建设水平、进度如何?在建设中，又会面临哪些问题?<br />
<br />
　　据介绍，28个已获批复的城市申请建设的地铁都在7条以上。到明年，我国城市轨道数量将达到55条。2020年到来的时候，中国28个城市建成运营的线路将达177条。但即使是这样的速度，专家们还是觉得慢了!<br />
<br />
　　中国工程院院士王梦恕教授：应该说是高潮期，但现在地铁发展，不是说有钱没钱，城市堵塞到这种程度，不修不行了，逼着你要修，这个应该说晚了。<br />
<br />
　　国家主管部门的态度显得冷静的多，健康、有序的发展是他们更愿意看到的。<br />
<br />
　　国家发改委基础产业司正局级巡视员李国勇：我们现在报国务院的同时，有很多条件，国务院有规定的，年GDP总量要在一千亿，三百万人口、财政收入一百亿的前提下才能申请。<br />
<br />
　　专家预测，未来30年，将是我国城市轨道交通建设快速发展的黄金时期。但风险无处不在。根据测算，地铁每公里造价高达5亿元人民币，而且建成通车后每年还有相当金额的运营成本，投入大、收效慢，巨大的资金压力对地方财政来说是不小的负担。这样的案例不是没有，南京地铁自建成运营后就面临巨额亏损压力，有消息称年亏损额可能就高达上亿元。此前南京对地铁一号线期望日收益值为200万元，但目前实际只能收到60多万元，和预估数字相差甚远。投巨资建设的地铁很可能成为&quot;烧钱&quot;的无底洞!<br />
<br />
　　&quot;没有地铁的城市是一个不完整的城市&quot;，这句话一度被广泛引用，当冰冷的钢轨再度被建设的热情点燃，城市的决策者们在享受兴奋和喜悦的同时，也不妨问问自己：有没有足够的财力养一条地铁?有没有足够的经验和智慧让它盈利?只有找到了肯定的答案，才能信心满怀地张开双臂，迎接呼啸而来的&quot;地铁时代&quot;。<br />
