													<h2>姜维平:重庆惊爆“重磅炸弹” 薄熙来杀人灭口（图）</h2>
						
						
																											<div id="postmessage_351813" class="t_msgfont">中共重庆当局新闻办发布一则令人震惊的消息：被羁押在重庆第二看守所里，正等待法院审判的的犯罪嫌疑人乌小青于11月28日中午时分上吊自杀身亡。（网络图片） <br />
【人民报消息】据新华社11月29日报导，重庆市政府新闻办发布一则令人震惊的消息：被羁押在重庆第二看守所里，正等待法院审判的的犯罪嫌疑人乌小青于11月28 日中午时分上吊自杀身亡。基于目前中共高层权斗的激烈程度和薄熙来的焦虑困境，这个消息如同一枚重磅炸弹，令世人惊愕。我认为，乌小青的自杀是一个精心策划的阴谋和骗局，它充分显示了薄熙来搞的所谓重庆反贪打黑不过是中共内斗的一场腥风血雨的运动。<br />
假如我没有坐过牢房，我会轻意地相信新华社的谎言，但据我所知，象中共官场上乌小青这样副厅局级干部，能在控制严密的看守所里自杀，绝对是天方夜谭。他被捕前系重庆市高级人民法院审判委员会委员、执行局局长，官至副厅级，此前他于2009年6月9日，被重庆市人民检察院以受贿罪和巨额财产来源不明罪，立案逮捕。乌小青被检方认定，其利用自身掌握的司法权力，为重庆黑恶势力谋取利益，是先双规一段时间后才送到看守所的。我知道，中共内部档明确规定，副厅局级以上干部的抓捕，必须首先由其所在的省常委会集体讨论通过，才能实施，那么，既使薄熙来能操控相当于省级的市委常委会，但他不可能不让一班人知道乌小青的关押状况，依我对薄熙来思想性格的了解，他会对别人严格保密关押地点，但不论如何，他不会把一个副厅局级的犯罪嫌疑人，关押在一个不能达标的看守所里。<br />
<br />
我们先来看一下重庆市政府新闻办发布的通稿，他描述乌小青自杀身亡的细节是这样的：“11月28日12时31分，被羁押在重庆市第二看守所的犯罪嫌疑人乌小青留下遗书后，趁同监舍被羁押人员午睡之机，避开监控录影，用棉毛裤裤腰绳，在内监门处上吊自杀。13时12分，同监舍的被羁押人员发现乌上吊自杀后报警，看守所值班干警和医生立即赶往监舍现场处置施救，并迅速将其送往医院抢救。15时40分，医院宣布经抢救无效死亡。”<br />
<br />
我认为，重庆第二看守所正是我上述所谓一般性的标准的看守所，如同我被关押过的大连三个看守所一样，它们是：大连海军基地看守所（部队），大连开发区看守所（区级），大连看守所（市级），也就是说，不论是部队的，区级的，还是市级的看守所，我都有切身体验，新华社的细节描述暴露了以下破绽：<br />
<br />
第一，所有的看守所均有规定，犯罪嫌疑人睡觉时，必须有两个人同时站岗，不用说中午，连半夜也绝对不能例外，何况走廊里还会有狱警不时地巡逻。第二，看守所的监舍空间十分狭小，一般分为大小两种，一种可关押八个人左右，一种可关押十二人左右，不论如何，只要所内警方安装了监控设备，就绝对不会存在所谓“死角”。既任何人无法躲避“猫眼”的跟踪监控，既便是蹲在厕所里大小便，也不能幸免。象悬梁自尽这样的举足轻重的大动作，不可能在中午的监舍里完成。他更不可能在监舍门前自杀。第三，看守所的房间里根本不会有用于悬挂东西的房梁物品或铁钩，乌小青体重不会低于一百斤，上吊自杀首先要找到能够承受体重的比较坚实的物品，这是难以想像的情况。第四，从入看守所的第一天开始，所有的犯罪嫌疑人就被警方没收了腰带和绳索，连衣服上的拉炼都要被剪下，所以乌小青绝对不可能拥有一根粗得足以能够承受他的体重的裤腰绳。第四，报导说，他留下了一封遗书，这说明了他死前有足够的时间写东西，做准备，那么纸和笔是从哪里来的呢？据我所知，一般情况下，犯罪嫌疑人不允许拥有笔和纸，如有，必定是专案组或狱警方面有人要求他写什么留下来，这正好进一步泄露了秘密：他不是自杀，而是别人授意，精心策划的非正常死亡。当然其细节还有待深入调查。<br />
<br />
至于官方的报导之所以要不厌其烦地罗列具体时间，那是为了扰乱读者的思维和判断能力，企图掩盖事实的真相。实际上，象薄熙来这样蹲过5年秦城大牢的官员，不可能把乌小青关押在低于一般标准的看守所里，因为那样将会被其它与之有过节的常委抓住把柄，他不会那么傻。比如2001 年他下令把他的政敌，原大连副市长，总工会主席高姿关押在一栋位于大连党校附近的秘密小楼中审查，把他的秘书，原大连中级人民法院副院长刘晓滨先后关押在大连姚家和沈阳苏家屯看守所里逼供，但由于辽宁省委书记闻世震等人的杯葛，使他的阴谋诡计没有最后得逞。否则高姿，刘晓滨副院长就可能和乌小青一样死亡。<br />
<br />
我估计，薄熙来及其马崽杀害乌小青灭口，是和刘峰岩带领的中共中央第三巡视组有关，因为薄熙来原先策划的目地是想通过抓捕文强，乌小青等人，深挖汪洋，贺国强等团派人马的问题，以此挑战胡锦涛，如同2004年江派抓捕田凤山一样，但后来在17届4中全会上，他们的权斗暂告失败，不得不退为守势，薄熙来才把打黑除恶的功劳与重庆的历任领导分享，所以原先从乌小青口中得到的东西，就成了不能使用的累赘，他的坦白材料反过来变为薄熙来，王立军整人的罪证了。所以乌小青难脱死亡的结局。至于如何死法，研究了一辈子《三国演义》三十六计的薄熙来一定胸有成竹。我想，可能如同2002年，薄熙来在和闻世震的争斗中，关押并害死原辽宁省监狱管理局局长郭建国那样，以他主动自杀追诉中止，可以返还已没收的巨额钱财给妻小为诱饵，逼得乌小青先留下遗书，再故意令马崽给他创造了死亡的条件，等他死后，再用优厚的物质条件堵住乌小青家人的嘴，然后威胁犯人写下目击证据，嫁祸于两个狱警，再等到事件平息之后，把他们重新安排工作，使此案永远成谜。薄熙来这一套精采而卑劣的表演，已在大连以至辽宁省多次获得成功。重庆不过是以往他恂私枉法的故事延伸而已，只不过在《三国演义》里叫借刀杀人，在这里叫监管不善，畏罪自杀，而对文强则不需要这样，因为只要不异地审判，在薄熙来控制的范围内，根据已掌握的证据，足已判他死刑杀他灭口。<br />
<br />
据经济观察网报导，乌小青是目前重庆市因涉黑落马的几个主要官员之一，其官阶之高，所在单位之特殊，为近年来罕见。此前重庆检察院认定，在2006年的一起土地拍卖案中，涉黑团伙陈坤志、龚刚模使用黑恶手段操控拍卖，并与张|（原重庆市高院副院长）、乌小青相勾结，导致该地块最终被低价拍卖。公开的资讯显示，龚刚模一案将于2009年12月15日于重庆市第一中级人民法院开审。<br />
<br />
该网文章对开审前乌小青的自杀也提出了疑问，它说，乌小青究竟是否“畏罪”自杀？按照乌小青的专业背景（其兼任重庆高级人民法院法官进修学院院长），对于此前检方提交的证据――“1998年到2008年，乌小青先后索取、收受多人贿赂共计人民币357.5万元、港币10万元，涉嫌受贿犯罪。同时还查明，乌另有518万元人民币不能说明合法来源，涉嫌巨额财产来源不明犯罪。”乌小青十分明白自己有罪，但很有可能不被判处“斩立决”（死刑立即执行）。那么他为什么要选择自杀呢？<br />
<br />
显然，很多人已经看出了其中的“猫腻”，但接下来薄熙来自有收场的办法。所以新华社的报导说，两名值班狱警已被控制，薄熙来又要表演周瑜打黄盖的闹剧了。<br />
<br />
不过我相信，不论是刘峰岩下重庆，还是黄奇帆忽然取代王鸿举，都显示了中共高层的内斗正在进一步加剧并充满变数，很可能在18大召开的前夜形势才能明朗。薄熙来绞尽脑汁作恶多端，不可能永远是得势者，他残害他人的卑鄙手段必将被政治对手学去效仿，反过来自已也身受其害。<br />
<br />
2009年11月30日于多伦多</div>

							
							
							
							
