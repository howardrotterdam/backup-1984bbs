													<h2>姜维平：由薄熙来公子的“艳照”想到往事</h2>
						
						
																											<div id="postmessage_100818" class="t_msgfont">偶然间看的一片文章，本人纯转。<br />
<br />
曾任香港《文汇报》东北办事处主任的姜维平先生，因在1999年6月到9月期间写了批评当时大连市市长薄熙来的文章，被薄熙来编织罪名，于2000 年12月被中国大连国安局逮捕。2002年，姜维平被以涉嫌非法向境外提供国家机密罪和煽动颠覆国家政权罪判有期徒刑8年，后减刑为6年。加拿大联邦公民、移民部暨多元文化部部长肯尼（Jason Kenny）为姜维平签发了部长特许签证，姜得以来到加拿大与妻女团聚。<br />
中共中央政治局委员，重庆市委书记薄熙来之子薄瓜瓜，在英国伦敦牛津大学读书，参加由英国华人青年联合会举办的一项比赛，并被评选为所谓的英国10大华人杰出青年之一，对此我心情平和。尽管我才离乡出国孤陋寡闻，对这个主办单位的权威性与受众范围，一无所知，但孩子们能出国读书并积极参加社团话动，并得到嘉奖，都是好事，可喜可贺。看到当年印象中瘦小的瓜瓜出落得风度潇洒，一表人才，不禁称奇。笔者虽被其父加罪五年牢狱之灾，但并不怨恨其子，只是5月12日诧异地见到中国各个网站刊出的薄瓜瓜的艳照，不胜感叹之余，才想起有关其父及子当年在大连的几件如烟往事。<br />
索要珍贵船模 经理敢怒不敢言<br />
早在80年代初期，我在《大连日报》文艺副刊做编辑时，就经常去金州约见业余作者，并有机会见到县委书记陈美良等干部，而薄熙来则当时不过是二把手，他给我的第一印象是对人谦虚热情，但区委干部们却说他表里不一，爱耍小聪明，占小便宜，且特别记仇。一个在区委工作的业余作者告诉我，薄暂居金州部队一套住房，但亲友到访，则免费下塌县政府属下的金州宾馆，而幼小的瓜瓜便成了那里的常客，很多宾馆服务员都熟知其人，我也在不同的场合碰到过他，感觉很失望，他疲弱瘦小，性情阴郁，动不动哭闹，有人说他严重缺锌，我则不加理会。因为现在每个家庭都只有一个小皇帝。我自已的女儿亦很任性，与其大同小异。<br />
不过，另外有一个干部对我讲叙一件事，使我震惊。他说瓜瓜由其父母带领，时而住在金州宾馆，有一回在金州宾馆餐厅吃过海鲜大餐之后，路过大堂，看到一件摆放在柜台里的工艺品，十分喜欢，驻足观望。那是一个价值千元的红木船模，不仅做工精美，而且仅此一件，薄瓜瓜提出要玩，父母笑而不语，陪同左右的宾馆领导，得罪不起，只好笑脸赠送。我听了此事，并不立即相信。因为做为京城下派干部，遭到地方官员嫉恨传谣，也在预料之中。然而，1997年我随大连国旅组织的一次为期15天的东南亚之游，使我意外地邂逅大连金州宾馆多名经理员工，闲聊中得知10多年前瓜瓜索要船模一事属实。一位经理说，薄熙来一家把宾馆当成自已家的，吃喝玩乐，三亲六故，请客送礼，常年不断，船模的事不曾忘记，因为那时价值千元的东西，并不多见。不过拿个船模也不算什么，拿了，那叫瞧得起我们！另一个厨师长说，船模很贵重，薄瓜瓜拿去玩，我们都只能是敢怒不敢言！<br />
后来，类似这样的事例在金州以至大连传言很多，无法一一证实。总之，不论是童年时代在金州，还是少年时代在大连，在北京，在新加坡，薄熙来之子薄瓜瓜都是在中国老百姓，敢怒不敢言的氛围中度过的。<br />
大闹表演赛场 令观众瞠目结舌<br />
90年代后期，我应聘到香港《文汇报》东北办事处工作，因采访需要，一年一度的大连国际服装节，不论有多少具体活动内容，组委会都会邀请我参加报导，所以每年我都出席在人民文化俱乐部搞的全国中青年服装设计大奖赛。有一次，台上的表演正在轻歌妙步中进行，3000名观众鸦雀无声，灯光下人们看到华衣美女飘飘欲仙款款走来，正在如醉如痴之际，忽听一声狂叫，一个沙哑的童音尖厉地响起，吓了我一跳，我坐在11排，只见5排中间一个10几岁的男孩，不知因为何事，哭闹不止，薄熙来则从3排站起转身，满脸堆笑，朝男孩旁边的吴秘书打着手势，于是全场观众愕然。<br />
原来,，薄瓜瓜大闹会埸，又哭又喊，多亏秘书吴文康双手把他急忙抱起，举过头顶，一边大步朝外走，一边尴尬地说着什么，表情很是特别。薄瓜瓜像一只老鹰手中的小鸟，脚在乱蹬，躯体伸直，哭喊不止，一路引人侧目。薄熙来坐回原处，不以为耻，故做镇静。<br />
坐在我右边的《大连日报》一老记对我说，真厉害啊，不愧为是薄一波的孙子呀。另一个《辽宁日报》记者告诉我，薄瓜瓜虽然居地在大连傅家庄附近的仲夏园，不属于西岗区一带学区，但他爹一句话，被破例送进大连最好最难进的实验小学就读，拍马屁的校长与教师，把他捧为掌上明珠。不久后，谷开来（注：薄熙来的第二个老婆）又托大连某房地产公司老板，把薄瓜瓜送到新加坡去读书。这位记者说，做为全国闻名的市长就培养出这般蛮横无理的小王子，你说老百姓怎么看！<br />
面对商贩惨遭毒打 表情异常冷漠<br />
90年代后期，薄任大连市长后，又占据长江路598号搂28层三套住房，我在其南侧福德街60号居住，故我们算是邻居。但并无私人交往。我驾车出行经常与他号码为辽B00051的坐驾擦肩而过，他的司机王某我已熟知，但薄瓜瓜一般不会坐在车中，他通常由安全局党委书记车克民亲自驾车接送其上学下学，故难得一见。<br />
事有凑巧。大约是1998年某一天上午9点左右，在福德街农贸市埸一带，云集了一批从乡下近郊赶来的商贩，他们为养家糊口，远道而至，明知市政府不允许他们路边摆摊卖货，但不得不一次次冒险。实际上，在薄居住的大搂与我的寓所之间，是一个并不临街的胡同，假如开放市场，不论小商小贩，还是周围百姓，都受益赞同，而且并不影响大连市容。但薄为了清静，竟下令打手――大连综合治理办公室主任彭某带领部下，对商贩大打出手。这回这个令人愤慨的埸面活生生地展示在我的眼前：薄出门一眼瞧见不听话的商贩挤满了街道，立即猛然大怒，下令彭某出发，自已上班离去。不一会，薄叫来的综冶办的两辆车匆忙赶到，一辆130小货，一辆金杯面包车，下来7个人，朝男女小贩追去，抓住他们又踢又打，并折断秤杆，设收货物，一时鬼哭狼嚎，路人围观。这时我一眼看到了薄瓜瓜，他站在不远处一辆银灰色进口房车前驻足凝望，脸色白亮，发型时尚，十分冷漠。我忆及1996年，新华社大连支社记者李某撰写的一篇描写薄夫妇及儿子瓜瓜的文章，充满令人肉麻的溢美之辞。其中叙说有一次，上小学的瓜瓜看到有一辆汽车在马路上抛洒垃圾，立即向父亲举报，薄认为这是见义勇为，大加赞赏。随后严肃查处了那辆车的司机。我想，在街头打人骂人，公然抢夺商贩财产的行为，比那个不小心丢弃垃圾的人，不知要严重多少倍，薄瓜瓜为什么无动于衷！<br />
公子一人到访 竟让干部久候<br />
80年代初期，薄与前妻，即原中共中央政治局委员李雪峰之女李某离婚，扔下一个儿子，接近薄的人透露，童年时薄并不大喜欢他，但90年代后期薄良心发现似有愧疚。有一次，当上大连市长的他，打电话给大连开发区管委会主任高某，对他说，过一会儿有重要来宾见他，叫他等候。但从早晨8点半等到 11点，那人才姗珊来迟，只见一年轻小伙子气度不凡，伶牙利齿，侃侃而谈，似曾相识，原来是薄的大公子，即与前妻所生的李某，他是利用学校暑假从北京到大连旅游的。高某后来笑着对我说，我还以为是什么大领导哩！反正有招待费，安排他吃个饭也没啥，就是装腔做势叫人心里不舒服！他还告诉我,他儿子到大连玩，被薄安排住在市教委下属的雅风宾馆里，但这个儿子与薄瓜瓜不同，不仅很善解人意，而且不贪婪不骄横。我想这大慨与其父母早年离异饱尝人生苦味有关吧！<br />
同一个世界 不同的梦想<br />
去年在举办奥运会之前，我还在国内读书与观察，已听熟了《同一个世界同一个梦想》的歌曲，亦不由得怦然心动。记得一度网络上刊出一张照片，上面是薄熙来、谷开来与薄瓜瓜的合影，看得出是在国外拍摄的。同时又有文章报导薄瓜瓜拿了英国哈罗公学校全额奖学金，并当选上欧洲先生等等，此前又有一篇文章介绍***的女儿到汶川地震灾区做义工，等等。有一天，我在搜狐网上读到某博友写的题为《同一个世界不同的梦想》一文，虽不过千字，但笔锋锐利，妙语连珠，一针见血地批评嘲讽了薄家父子，不仅质疑薄瓜瓜入读英国牛津大学高昂学费的合法来源，而且尽举山村失学孩子的遭遇与梦想为证，得出同一个世界不同的梦想的结论。可惜这篇力作出笼不久即被网警删掉了。<br />
然而，永远删不掉的是中国目前贫富两极分化的严峻现实：一方面是类似薄这样的高官，身居重庆市委书记的高位，又是唱红歌，读红书，教导农家子弟艰苦奋斗，天天向上，争做革命红色接班人。另一方面，却利用手中的特权，大肆搜刮民脂民膏，远送子女去美国与英国接受资本主义的西式教育，吃喝嫖赌，极尽奢侈。在薄瓜瓜头顶所谓英国十大华人杰出青年桂冠的同时，在中国广大农村的穷乡僻壤，有多少孩子连书包，书本都买不起，连课桌都没有，连饭都吃不饱！试问，难道他们的智商比薄瓜瓜低？难道他们都不比薄瓜瓜长得漂亮？难道他们都不是人？薄熙来让别人的孩子去学雷锋，自已的孩子跑到英国去泡洋妞，这是人格分裂，还是骗子惯技？他这种人怎么能够领导中国走向民主与法制？薄熙来之子薄瓜瓜能代表英国的华人杰出青年吗？ <br />
