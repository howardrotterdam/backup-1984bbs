													<h2>64――民主之花抑或文革激情的延续？</h2>
						
						
																											<div id="postmessage_597530" class="t_msgfont">很少来84，这一来就看见64文章集中度非常之高，还原历史，回归理性好像成为大家的追求，但我看见的只有傲慢与偏见，当我们不再带有情绪以最为客观的历史视角来看待历史的时候，那个时候你可能成为社会的守夜人。但好像鄙人也做不到这点，在读完相关的一些史实资料（至于是否偏颇鄙人不敢断言），我是大哭了一场，真正的一个人在屋子里面痛哭那种，不过绝对不是为几个民运的人哭（直到现在我更坚信这帮专制文化培育下来的是一帮垃圾，在此我有情绪的），我是为这个民族为这个世代接受专制和奴役的民族而痛哭流泪。<br />
&nbsp; &nbsp; 很多人直到现在还那么肯定64甚至拔高其道义行为，我想不是别有用心就是根本就不懂得民主自由政治的要谛。我也不敢肯定自己就是绝对地懂，但我只想尽量做一个守夜人的角色，做一个真正有担当有正义的人。我想在这篇文章出来之后肯定会挨砖头，不过我还是不得不说出我心里真实的想法以寻求更好的办法解决民主民族民生民权。我愿尽量做到站在公正的来立场上来评述此事，以求达到抛砖引玉的效果。如果那些自以为代表正义的采取专制手法来索取本人性命之时，最好想想你自己的后果。因为我已经准备好了牺牲，但绝对不是为了你么而牺牲，而是为这个民族的自由和民主。<br />
&nbsp; &nbsp; 64这场运动应该来讲鄙人并不是亲历者，但作为关心民族命运和民族自由的人来讲，我同样也关心着这场运动它带来的实际意义，它究竟给我们带来了什么，最早接触相关资料是有关64运动的一些影音资料，这些资料都是从一些地下渠道获得的，当然我一直想能够获得更多更全面的相关资料。<br />
&nbsp; &nbsp; 这场运动其起因很多人都已经知道，很多人也了解了相关的一些背景，这场学生运动的过程诸位参与者也经常在不同的场合反复强调。但吊诡的是，我们的背景资料从来都是基于当时的一些激情和道德情操，甚至我怀疑这种激情和情操其来源就是不合法的，是一种共产主义专制教育下的道德延续，是一种带有浓厚专制传统的情怀的一种迸发而已。更多的人缺乏从民主政治自由经济及社会环境的视角来看待这件事情，我就不得不问，这样的分析是否经得起历史长河的考验。所以我们也就难怪看见柴玲自以为是的痛骂其他留学生的恶俗态度。<br />
&nbsp; &nbsp; 应该来讲64确实和当时社会政治经济文化这些深刻的社会变动因素是有关的，在64之前的中科大的学生运动在本质上就没有其深刻的社会利益追诉源，从某种意义上讲仅仅是因为天之骄子的突然堕落而已，因为我们的知识分子群体已经官僚化了，他们为了失去的利益进行的所谓抗争而已。也有朋友给我讲了，当时的科大的运动和解放之后的学界极其反感政治干预有关系（方励之和胡乔木进行的唯物主义和唯心主义之争）。但我更多的是看见那时候由于国务院发布的“大学生宽进严出”的大学招生制度有关，这样的政策无疑影响了大学生自建国以来（除开文革期间）的天子骄子形象，这种身份的失落感深深地影响到每一个具有浓厚官僚情节的知识分子群体身上。也是在这个时候，社会弥漫着强烈的反传统否定民族性的一些所谓自由化意识形态，应该来讲，这种情绪的蔓延同时也助长了嬉皮士运动，至少就我了解的当时的学长们那种进入大学就万事大吉、那种跳龙门的思想仍然在作怪，最为严重的表现，据我个人了解，很多学校的学生都逃课、整日麻将、叛逆、花前月下（当然我读书的时候也有）、怪异的头发、喇叭裤这些也逐步成为当时的时尚，我们开始接受了西方年轻人在60年代盛行的嬉皮士之风气，但在我看来这种风气更是一种表面追求而无内涵实质的一种东施效尤的一种行为而已，或者说接近于犬儒主要的行为风尚而已。当然我并不主张所谓的大一统思想，事实上我更看重的是社会的思想多元化行为，更是希望大学思想圣殿堂成为学术多元化的论辩场，但可惜的是，我们弥漫在跟风、潮流、无独立见解的前信息时代，那种由于一个人带来的所谓的解放就成为我们更多知识分子群体的一种所谓追求，那种对河殇的论述的盲目崇拜造就了一个时代的灵魂，那种对所谓的耀邦（专制体制内的守卫者）的崇拜也成为年轻人的喜好，这就是流行时代的妙处。我们更多的知识分子群体，我们的大学生仍然陷于了传统的跟风、守旧、个人崇拜主义这种文革延续下来的风气。这种习俗并未因为改革开放的改变而改变。<br />
&nbsp; &nbsp; 在经济上，由于在紫阳（我一直以来建国以来我最尊敬的总理）主政经济工作时期，80年代初期的改革确实是带来了社会经济的全面复苏，同时也带来了各阶层的利益的增长，最为明显的就是农村经济的全面振兴，记得小时候农村那种鸡犬相闻、炊烟袅袅的景象让我到现在仍然难以释怀。同时由于农产品价格的初步放开，这也带来了城乡差距的足部缩小，通过中国经济年鉴我们可以发现，80年代城乡差距是建国以来最小的年代。在乡镇企业方面，各地也开始逐步开花结果，这个时候不仅仅是沿海地区，同时中西部的乡镇企业也得到飞速发展，《他乡的税收》这本书真实地记载了河南一个小镇的兴衰就说明了这问题。在城市，虽然当时的万元户还未能真正地进入到城市阶层，由于城市国营企业和乡镇企业竞争劣势决定，我们看见了国营企业出现了一些亏空现象，也正是由此原因引发了后来朱主政时期的国营企业股改（这个具有深重灾难开始的所谓改革的开始），但由于此时城镇居民的福利保障相对完善，这个时候也能看见城市人的幸福日子（当然我反对这种福利化模式），也正因为农村经济的复苏，乡镇企业的发展，计划经济的相对弱势，这样我们看见社会的物质财富确实相对极其丰富，这个时候的人们也看见了生活的希望。但由于更为深层子的专制集权制下的经济发展(颈，我们看见了在88年期间，中央不得不采取“价格闯关”的办法以解决日益恶化的中央财政危机(这也和当时的中央和地方的财务分享机制不完善有关系，以至于后来我们走向另外一种极端），这场价格闯关是一种社会经济转型时期的一种必须，是一种必然性措施，但由于这次价格闯关的具体操作模式存在着严重的问题，它也制造了当时的通货膨胀，也侵害了不少利益群体的利益，特别是相当部分是传统的利益群体工人、知识分子、以及刚刚平反得原tg统治群体的利益。但即使这样，它仍然未造成社会的大面积崩溃或者滞涨现象，当时的金融机构也采取了一些高息揽存的措施避免通货膨胀的进一步扩大化，避免出现挤兑和社会抢购风波。但也正是这场“价格闯关”它滋生了社会的强列不满性情绪，从本质上讲这是转型期间一种政治和经济制度出现不适应的一种表现，它表现为守旧的福利享受集团的高度愤懑。这恐怕是其经济上的一个极为重要的因素。但由于社会的整体获利现状，民并不具备社会改变的强烈意愿及源动力，甚至更多的是想回归到原来的大一统的计划经济经济模式上去。<br />
&nbsp; &nbsp; 同时在政治上，我们也看见，守旧的以陈云为代表的传统计划经济官僚阶层仍然不失时机的对以赵威代表的经济革新派的反扑，其最为有代表的大家都知道是当时的左王邓逸群，这个高举马列旗帜的中共最高宣传长官，不遗余力地展开对经济改革以及政治改革的攻击，而其幕后黑手应该是当时的陈云。在此时进行的政治改革可以说已经进入了一些实质性的操作，首先开始的是党政分家，这样的党政分家已经进入了实质性阶段，很多地方政府及党组织的利益划分管辖权力已经分配得非常的明显，在当时开来已经是相当大的进步，和我们现在的党政结构比较起来都是极大的进步，甚至可以说现在的公权结构重新回到了黑暗的时代，这样的政治改革更是触及了一些官僚阶层的根本利益。这样的激进改革是有当时代表党中央最高权力的两任书记耀邦先生和紫阳先生逐步进行推进的，这也是由于他们体会到经济改革和政治制度不相适应的一种必然性选择。同时我们至少还听闻紫阳先生提出的党内分派、民主选举这样一些完全有别于专制集权政治的提法。<br />
&nbsp; &nbsp; 同时在80年代，思想自由之花确实已经展开，不管我们是否愿意，是否认可，至少我们看见从未有过的思想激辩在逐步产生，思想界的多元化现象得以在当时社会形成。这种百花齐放百家争鸣的应该来讲是49年之后难有的。即使从64本身来看，我们也能看见央视的全天候的直播。试问今天的我们有吗？群体性矛盾逐步增加，真理部加强言论控制，较之法西斯有过之而无不及。这样的现状是怎么导致的呢？我们是否应该反省一下呢？<br />
&nbsp; &nbsp; 我个人不想猜度当时所谓的学运领导人的自我心情，我不削于纠缠柴玲是愿意牺牲生命还是为了私利，这些都不是我们追溯的目的，还原历史真相的是需要的解决未来民主自由进程的方向。<br />
&nbsp; &nbsp; 从64的动机来看，由其最开始为了耀邦先生祭奠行为逐步演化为全北京、全国的一场学生运动，这样的目的显然是可笑的，它至少说明了一个问题，那就是这群比较二的学生还沿袭了传统的个人崇拜主义，将历史的使命担当归为某些英雄式的人物身上，虽然其后期发展为反贪官反官僚这样好听的诉求上来，但其本身都不具备真实的利益追求属性。这也正是其失败的最为根本原因。利益诉求不明确，反反复复，以专制捆绑的手段来诉求民主的行为只有一个结果，暴力相互施加。<br />
&nbsp; &nbsp; 前面我已经讲过当时的经济状况，社会的整体获利行为其实产生了更多的人维护现有体制状况的行为惯性，但同样也由于这样的转型导致了一部分利益的损害，这也决定了某些人必然借助这样的运动以期达到对改革的反击，事实上89-92之后左的反扑也基本上说明了此问题。由于更多的人在改革当中带来的经济利益的增长，绝大多数是希望是进行经济体制的深化改革，但只有明白之人才知道，这样的危机更多的是政治制度造成的。我们不得不遗憾地认识到，当时有这样的先见之明的人并不多见，更多的文人进行了所谓的54以来的传统文化的反叛。我想大概能由这样认识的也只有当政的紫阳和其麾下的智囊团。任何社会的进步不可能依靠激情来推动的，它必须有赖于社会各阶层利益的诉求表达来推动，而我们却没有这样的社会需求，反而我们某些无知的行为使改革噶然而止。而真正产生这种反对的利益动因的话，我想只有一个了，受损利益团体，这样我们的这场运动无疑充当了反改革的利益团体急先锋。当然我有时候猜测，当初他们开始打到紫阳的那种行为到后来又口口声声说紫阳要他们坚持的这种鬼话是怎么编出来的？<br />
&nbsp; &nbsp; 在行为上更是几近于荒唐，从4.26社论发表之后，这种在专制集权下的对社会动荡的批判我们应该抱有起码的戒心，试问在不合法的政权下追诉合法的理由，这些行为就显得极为的幼稚。更为可怕的是，以学生的身份和主体和国务院进行所谓的对话，这样的对话有基础吗？我们只看见双方都在放空炮，找不准目标，你是要tg下台呢还是要选择你们新的崇拜偶像呢？这样的行为和文革的打到一切又有什么差异呢？在我看来更像是文革癫狂病的最后一次爆发而已，这样的行为不过是期望一个专制取代另外一个专制而已，因为我们至少看不见这里有一点法制认识基础，没有一点妥协意识，看到的只是对抗，李鹏是蠢猪，被打了鸡血的绑架学生感情的学运头子们又是为了什么呢？唯一我们看见后面的一些亮点就是反对官倒、反对官僚，但这样的口号却显得如此缺乏社会认识基础，我们没看见有那么一个倡导提议民主利益诉求，没看见一个寻求制度改变分配的倡议，真的就一个个这么蠢吗？西方民主自由理念在当时的北京大专院校里面也开始有很多的认识基础了吧。到后来其行为就更变得更荒诞不羁了，以所谓的绝食来威胁集权官僚，这时候已经成为僵局，因为当政者也不知道真实的诉求是什么，该答应的也答应了，可能他们想象王洪文那样的翻盘。这就完全成为一种捆绑行为，就如妓女向嫖客撒娇要换位一样。我们最后看不见任何有建设性的意见或者目的，这样的行为才是可怕的，因为都盲目了，都开始为了反对而反对。<br />
&nbsp; &nbsp; 结果呢?我们看见了，言论自由的倒退，社会警察化，利益官僚化，政治更为专制化甚至世袭化。这都是因为你们的行为，你们使官僚走向利益的巅峰。<br />
&nbsp; &nbsp; 这是花还是文革激情延续，我也不想给出最后的答案，让大家评判吧。<br />
&nbsp; &nbsp; 最后我对64逝去的同学们致以深深的哀悼！64应该平反，但绝对不是给那部分绑架同学们感情的垃圾们的平反。愿逝者安详。中国的民主进程不应该被一些无谓的行动所葬送，我们在这个时代应该挑起大梁，追求自由是我们的理想，建立民主政治，实现宪政，建立多元化社会，实现社会的利益合理公平分配。<br />
&nbsp; &nbsp; 如果有人想拍砖的话，可以加入花木蓝的q群找我。我奉陪到底。<br />
&nbsp; &nbsp; 不好意思，帮花木蓝打了下广告。</div>

							
							
							
							
