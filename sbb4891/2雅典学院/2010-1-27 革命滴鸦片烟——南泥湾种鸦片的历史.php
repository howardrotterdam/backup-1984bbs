													<h2>革命滴鸦片烟――南泥湾种鸦片的历史</h2>
						
						
																											<div id="postmessage_454649" class="t_msgfont">一、一点疑问 <br />
<br />
　　首先有一个疑问是长久使我感到困惑的，即战争时期我军的后勤问题是如何解决的？如果说内战时期可以用打土豪、斗地主、分浮财的办法来解决，那么抗战时期呢？那时搞了统一战线，不好打地主土豪了，此其一；根据地大多都很贫瘠，难以想象能够负担得起这么多军队和工作人员，此其二；抗战进入相持以来，我军基本上都是遇鬼子就躲，躲不过去才勉强打几仗，唤为“反扫荡”，更谈不上去收复日军侵占的失地，获得可观的物资补充，此其三。而日常消耗还是很大的，据说八路军每个指战员每天要有5分钱的菜金、1斤半粮食，此外每月还有战士1元、干部2元的津贴。到相持阶段，我军在各处扩编已达几十万人，再加上干部、工作员、民兵等等，少说也得百来万吧。即使是最低消费，每个月总得开支人头费不下数百万元，粮食数千万斤吧。这些如何解决？有人说不是搞大生产吗？比如南泥湾... 打住打住，南泥湾的产量我去查过了，1939年陕北留守兵团全年开荒25136亩，收获粮食38.85万公斤；1940年全年开荒20679亩，收获粮食36万公斤。每年收获的这点粮食大概仅够留守兵团自己自给一个半月。难道我党人士真乃特殊材料制成，不吃不喝居然也使得革命不断壮大乃至最后胜利胜利？近来看到一些文章，似乎找到了一点答案，发现“土特产”――大烟对于革命实在是功莫大焉。<br />
　　我党我军对大烟的使用早就不是什么秘密了。例如在土地革命时期，就经常将黄金、烟土这些体小易藏而价值大的东西作为地下活动的经费；红军在长征途中，一路上都是把大烟土当现金使 用，随时用烟土与所经之地的老百姓或国民党地方军队交换生活物资或枪炮弹药（索尔兹伯里在《长征-前所未闻的故事》里讲过：“有的红军战士回忆说，他 们曾用鸦片当作货币去购买生活必需品。” ），等等。不过，在抗战时期在边区种植和贩卖鸦片则知道的人较少。台湾陈永发教授在1990年发表的《红太阳下的罂粟花：鸦片贸易与延安模式》一文曾系统地阐述过这个问题，那篇文章俺没有看过，最近看了一些网络文章，又到超星上调出了《谢觉哉日记》和部分民国挡案文献等资料加以印证，略微查到一点蛛丝马迹而已。 <br />
<br />
二、老红军干部的回忆 <br />
<br />
　　有一位署名晓庄的曾在网络撰文提到采访一个陕北老红军的故事。该陕北老干部是当年刘志丹的部下，谈起改革开放后原“陕甘宁边区”的经济落伍，他便耿耿于怀。谈到陕西地区毒祸猖獗的问题，这位老干部更是气不打一处来。他用自己的亲身经历，说明了陕西地区的烟毒泛滥，正是当年抗战时期种下的祸根。 <br />
　　据这位老干部回忆， 国共抗日统一战线形成后，他被派到山西参与掌管一个抗日根据地的财政。一九四一年该根据地因为实在穷得揭不开锅了，不得不向延安方面告急。延安方面答应得非常痛快，没几天便派一支部队护送一批毛驴和骡子驮运物资到了山西。山西方面验货时才发现，除了几百件延安生产的土布军装外，还有几百斤大烟土。押运人员还带去了陈云的亲笔信，那信要求山西方面用这批烟土向当地国民党军队或日伪占领区换取必需的军事物资和生活物资。 <br />
　　这位老干部讲到此处，问晓庄是否会唱那首著名的《南泥湾》，然后又愤愤地说：“什么‘到处是庄稼，遍地是牛羊’，说到处是大烟还差不多！” 原来，这位老干部当时因为对贩卖大烟想不通，便被抽调回到延安抗日军政大学受训。根据边学习、边生产的原则，受训期间前往南泥湾，与王震领导的三五九旅一同开荒种地。粮食确实种了一些，但好一点的地都被用来种了鸦片（想想上面的数据，每亩平均产量不过二三十斤，也委实低了点，这说明开荒的土地的确不是全种了粮食）。而且，王震的三五九旅还专门雇来工匠，把收获的鸦片加工成烟土，然后交由陕甘宁边区财政厅集中保管，随时运往山西、河北等地，无论是国民党军守备区还是日伪占领区，谁给钱就卖给谁。事实上，因为日伪占领区很难进入，所以绝大部份边区烟土都被卖到国民党守备区，部份卖到民间，部份直接卖给国民党军队。 <br />
　　那位老干部说，他本人没有参加过长征。他在抗大受训时仍然表现出对我党作贩毒生意的无法理解，一些参加过长征的干部笑他“少见多怪”。那些长征干部还夸口说：“当年如果我们手里没有大烟土，早就饿死困死在长征路上了。” <br />
　　贩卖大烟的行当实在不是什么体面光彩的事情，所以边区根据地统一将大烟称为“土特产”。这种“土特产”交给山西、河北的八路军经营时，则一律称之为“特品”或“特种物资”。在笔者看到《第１２０师陕甘宁晋绥联防军抗日战争史》中曾写到：“留守兵团驻各地的部队.....积极开展贸易工作，主要输出一些食盐和一些土产，输入棉花、布匹、药品和一些自己不能制造的日用品。”时，还一直纳闷那光秃秃的黄土高原有何值钱的“土产”可以换来这大批的物资。看了上文，自然茅塞顿开。 <br />
<br />
三、革命的生意经 <br />
<br />
　　一般认为，从一九四○年冬开始，皖南事变前后，国民党政府就停止给八路军发军饷，用老毛的话说：“蒋委员长不给我们开饭了。”实际上，笔者怀疑，即使国府按原额准时地付给八路军军饷，也是杯水车薪。因为那时八路军已经扩展到几十万人了，国府当然不会按这个数字给钱吧。那时整个陕甘宁边区穷到没有饭吃、没有衣穿的地步。老毛情急之下，将南汉宸委为陕甘宁边区财政厅长兼边区参议会秘书长。南氏掌印后，就“新官上任三把火”，立即扭转了边区的财政窘境： <br />
　　第一把火是纠正“片面施行仁政” 的作法，组织征粮工作团下乡，让老百姓把手里的粮食“自愿”地交上来。例如有些地方志或文史资料中曾写道：“群众宁肯以野菜度日，也不让八路军子弟兵挨饿。”这真是绝妙极了。 <br />
　　第二把火是趁日伪军控制山西及苏北的池盐产地之际，集中收购陕甘宁地区池盐产地的民间产盐，同时组织部队打盐，然后将盐价提高一倍。山西方面的国民党部队不同意这一价格，南氏便下令不准外运，直到国民党部队的库存告罄，边区政府自然发了一笔盐财。笔者在《谢觉哉日记》中曾看到一些有关盐品买卖的有趣记录。 <br />
　　第三把火便是由边区政府统一进行所谓的“土特产经营”。 边区政府里一直有人反对经营土特产这种生意，有几位老同志还给老毛写了两封信，历数经营土特产的弊端。 另外，西北局的一些领导，包括司令员高岗在内，都是这个意见：“宁可饿死，也不能做这个买卖。” 为此，老毛又召见南汉宸，嘱咐说：“我们要向人民说清楚，向全体干部说清楚，使大家都了解，我们不得不这样做，完全是为了抗日和革命。为了这个目的，我们必须牺牲一切。” <br />
　　有了老毛的支持，南汉宸便依靠经营“土特产”筹措到钱财，用这些“土特产”从国民党地区交换过来革命所必不可少的军用和民用物资。一般认为，贩卖土特产的收入要占到边区财政收入的半数以上，个别时候要占到三分之二左右。南汉宸因扭转边区财政状况，“功”不可没，而得到老毛的特别器重。一次江青见到南汉宸夫人王友兰，曾拍著她的肩膀，竖起了大拇指说：“你的爱人真行，毛主席夸奖他，说他是这个！” 建国以后，南汉宸因有此救党大功，被任命为中国人民银行首任行长之职。 <br />
<br />
<br />
四、外人日记中的有关记载 <br />
<br />
　　塔斯社记者、莫斯科驻延安的特派员彼得.弗拉基米若夫尤在他的《延安日记》里对此有详细的描述。《延安日记》里曾写道：“到处在做非法的鸦片交易。例如，在茶陵， 远在后方的一二零师部，拨出一间房子来加工原料，制成鸦片后就从这里运往市场...” ，“政治局已经任命任弼时为鸦片问题专员...” <br />
　　当彼得问及毛泽东：“特区的农民往往由于非法买卖鸦片受到惩办，而现在甚至是共产 党领导的军队与机关也在公开地生产 鸦片---这到底是怎么一回事？”时，老毛没有吭声，一旁的邓发代毛回答说：“从前特区只是把盐和碱运往国统区。我们一挂挂大车满载著盐出去，带回来的钱袋却是瘪的，而且还只有一个钱袋。现在我们送出去一袋鸦片，就能够带回满满的一车钱。我们就用这些钱向国民党买武器，回头再用这些武器来收拾他们！...” <br />
　　下面的几段均摘自《延安日记》： <br />
　　“中共政治局甚至批准，要加强发展公营的鸦片生产和贸易...要在一年内为中央政府所辖的各省的市场（叫作对外市场）至少提供一百二十万两的鸦片...” <br />
　　“鸦片的事情，就是说罂粟的种植与加工，大部分将由部队来做管。贺龙的一二零师所在地是最主要的提供鸦片的地区（这个师已长期做这项生意）... ” <br />
　　“毛泽东同志认为，种植、加工和出售鸦片不是件太好的事情。可是，毛泽东同志说，在目前形势下，鸦片是要起打先锋的、革命的作用，忽视这点就错了，政治局一致支持中共中央主席的看法。” <br />
　　此外，彼得还写道：“解放区出现了一片怪现象。中共的部队同样也出现了这种怪现象。他们全都在尽可能地与沦陷区的日军做生意... 实际上晋西北各县都充斥著五花八门的日货，这些日货都是由沦陷区日军仓库所直接供应的...” <br />
　　从彼得的日记来看，鸦片的种植地区除了陕北外，晋西北也是个很重要的产区，主要还是因为这些根据地比较偏僻，日本人也来打扰得少，便于进行秘密、成规模的生产。其它一些根据地由于情况比较复杂，相对少一些（也不能说没有，比如后面附件里的就是冀鲁豫边区的，国民党挡案中也曾提起浙西根据地也有此行径）。 <br />
<br />
五、国民政府挡案中的有关记载 <br />
<br />
　　鉴于鸦片的销售对象主要是国统区，不能不引起国府各有关官员的注意。下面是从国民政府挡案中摘录的一部分有关记载目录概要： <br />
　　1940年10月29日，朱家骅、徐恩曾报告：山西共党合作社公然售卖鸦片等情 <br />
　　1941年7月16日，第42军军长杨德亮报告：中共迫商人贩卖烟土。 <br />
　　1942年7月12日，财政部公债司抄送中共于陇东摊派公债、贩运毒品等情报函 <br />
　　1942年7月21日，傅作义（第8战区副司令长官）报告：中共以种鸦片筹饷为由，拒绝国军派队巡查。 <br />
　　1942年7月28日，中央文化驿站榆林分站主任王廷龄报告：共党在晋西北广种鸦片情形。 <br />
　　1942年9月18日，朱绍良（第8战区司令长官）报告，中共在陕甘擅征盐税及禁止法币流通与倾销鸦片。 <br />
　　1942年10月2日，42军军长杨德亮报告：中共关中分区以鸦片抵发薪饷，每人二两。 <br />
　　1942年10月4日，朱绍良报告：中共于陇东推销鸦片，以庆阳之驿马关及合水之西华池为中心，分设土膏店。 <br />
　　1942年12月4日，傅作义报告：中共与日军进行交易的情形。 <br />
　　1943年12月18日，朱绍良报告：中共当前运销鸦片及强迫人民种植情形。 <br />
　　1944年3月25日，傅作义报告：中共令积极推销烟土。 <br />
　　1944年4月4日，朱绍良报告：中共于合水西华池设烟土公司，大量倾销鸦片。 <br />
　　1944年4月4日，朱绍良报告：中共在绥德广种鸦片，并公开出售。 <br />
　　1944年10月18日，河南省主席兼警备总司令刘茂恩报告：中共在豫鄂皖边区强迫人民种植鸦片。 <br />
　　1945年5月30日，顾祝同（第3战区司令长官）报告：中共于浙西擅设出口税局并大量种植鸦片。 <br />
<br />
六、《谢觉哉日记》中的有关内容 <br />
<br />
　　如果有人不相信国民党方面的报道或者外国人的记载，那么在负责边区政府事务的谢觉哉老先生的日记里到可以提供一个佐证。谢老在日记中把鸦片一律称为“特货”。下面摘录几条关于“特货”的情况： <br />
　　1）“就是特货一项得的法币占政府收入……尽够支用。”（1944年1月18日，从这里可以看出，仅“特货”一项的收入就足够边区政府的支出开销了） <br />
　　2）“特货多边币少，将来不得了”(边币收回，特货跌价，买特贷的不得了)”（1944年3月12日，“特货”之多，都到了跌价的地步） <br />
　　3）“领导机关发动一件事，必须十分考虑周到，常常一小步差错，在群众中可闹出大乱子。‘特货内销’即其一例。”（1944年3月14日，“特货内销”该不是指就地卖给当地群众吧？难怪会出大乱子。） <br />
　　4）“据调查边区内存的法币不下二万万元，无疑是由特货补足普通物品入超而有余来的。”（1944年4月9日，出售“特货”的收入减去购货开支，还有大量顺差，竟不下两亿元） <br />
　　从谢老日记中的上述记载中不难看出，“特货”的经营几乎是边区唯一的大宗收入来源，且使得贫瘠的边区得以扭“亏”为“盈”，是我党我军在八年抗战中赖以生存和发展的重要经济基础。 <br />
　　谢老的日记里还记了这样一件事情：有一个退伍军人，用其积蓄买了几两“特货”，准备外出出售，结果在关卡被查扣没收，于是“变穷了”，一贫如洗，回来后向边区有关政府部门要求经济补助。从这一事例至少可以说明两点：1）“特货”交易的确是赚钱的买卖，当时从事这一行业的人并非个别；2）“特货”不允许私人交易，而是“公家”的专利，这点和彼得日记中的描述也是一致的。 <br />
　　当年边区种大烟的秘事也曾得到当地农民的证实，当人们询问一些上了年纪的陕北老农时，得到的回答是：“那是共产 党让种的咧。” <br />
<br />
七、“为人民利益而死”的张思德同志 <br />
<br />
　　在国内，年龄稍长者都知道有个“老三篇”，一篇是赞扬神话人物的《愚公移山》，一篇是赞扬国际主义战士的《纪念白求恩》，还有一篇是赞扬一个士兵的《为人民服务》 。《为人民服务》提到中共中央警卫团战士张思德，在陕北安塞山中烧炭时因为炭窑崩塌而牺牲，他“为人民利益而死”，其死“重于泰山”。 <br />
　　但近来网上有人撰文，揭出张思德原来并不像《毛选》中注释的那样，在烧炭塌窑而死，而是在被派到当地一处大烟加工厂参与烧制大烟时，因为烟窑崩塌而被活埋而死的。为什么这种事要让中央警卫团的人去干？也不难理解，因为加工大烟的事情既要保密，又要保证参与这项工作的人不会中饱私囊，所以必须安排十分可靠、“党性强”、“纪律性强”的人去干。事实上，除了张思德， 中央警卫团很多干部、士兵都轮流参加过加工烟土的工作。 只可叹的是，烧烟英雄居然也被立作楷模，还让全国人民学习了数十年，也真是件让人哭笑不得的事情。 <br />
<br />
八、简评 <br />
<br />
　　这些东东过去在公开出版的书中当然是绝对禁止出现的，有关的记录均被删改无遗，几乎难寻其踪，恢复事实原来的面目难上加难。而且，长期以来的宣传，往往对为数不少的人造成一种“洗脑”效应，即不愿承认过去以来的某些客观存在，浮现在脑海里的总是教科书本上灌输的那些东西，这里面的是非曲直我们就不说了。但事情过去半个多世纪了，多少总要对过去有个客观的交待，不要再贻害子孙后代吧。就事论事，面对以上曾被尘封的历史事实，回想过去传统教科书里经常抨击某某地方军阀割据一方、种售大烟、残害人民、罪恶滔天云云，我们还能说什么呢？这也许就是政治的需要吧，非我等小民所能道哉。<br />
