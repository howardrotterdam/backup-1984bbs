高超本领：‘他买套房，给他爸妈买套房，他妈妈买张床也要17万；5、6个女友<br />
一人一张信用卡；今天买辆跑车，半个月后不喜欢了，折价卖掉……”（《专访<br />
韩寒经纪人路金波:投机主义的绅士》）<br />
