知味，说“不签售”的，却在淘宝上签售起来，并且还自吹一年签上万本；说不<br />
“出席时尚聚会”，却代言手表的广告发布会；说“不参加演出”，却在《宅居<br />
动物》、《海上传奇》中频频露脸；说“接受少量专访，原则上不接受当面采访”<br />
的，结果当面地、不当面地大量接受专访，从内地到港台、从中国到外国，从报<br />
