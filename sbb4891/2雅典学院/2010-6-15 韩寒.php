候在看《上海楼市》，发现上海的房子大部分我都买不起。我在写东西的人里面，<br />
收入算比较高的了。在一个这么大的国家里，居然连一个城市的普通公寓楼买起<br />
来都很吃力。我觉得不应该这样，我认为在一个国家，到这种级别的作者，别说<br />
公寓了，甚至法拉利都可以10辆10辆的买。这才是对文字的尊重。”类似的话，<br />
韩寒对多家媒体都说过。总之，他是名流、他是“顶尖的文化人”，他不该买不<br />
起房。一时间，“说真话”、“代表年轻人的心声”等美词儿，又四处招摇。<br />
<br />
