<br />
<br />
　　韩寒在博客公告，说：“不参加研讨会，交流会，笔会，不签售，不讲座，<br />
不剪彩，不出席时尚聚会，不参加颁奖典礼，不参加演出，接受少量专访，原则<br />
上不接受当面采访，不写约稿，不写剧本，不演电视剧，不给别人写序。”这一<br />
