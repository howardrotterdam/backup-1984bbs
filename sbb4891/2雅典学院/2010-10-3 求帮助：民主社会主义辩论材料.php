																											<div id="postmessage_854239" class="t_msgfont">要辩论此题，关键在于怎么定义“真正的社会主义”。估计对方的准备都是围绕这个模糊的概念在搞，所以你查下相关资料，对手会怎么说基本就心里有数了。<br />
网上查到两个定义：<br />
1、社会主义（英文：Socialism），是一种经济社会学思想，主张或提倡整个社会作为整体，由社会拥有和控制产品、资本、土地、资产等，其管理和分配基于公众利益。<br />
2、共产主义的初级阶段,在这个阶段生产资料主要归全民或集体所有,分配原则是“各尽所能,按劳分配”<br />
看到二者的分别没？<br />
作为经济社会学思想的社会主义，提倡整个社会作为整体。而在实际操作中，作为共产主义初级阶段的社会主义，实现目标的手段是坚持党的领导，暴力维持政权并实行专政。<br />
民主社会主义有民主两个字在，它实现“整个社会为整体”的路径是民主，而不是专政。<br />
简单的说，就是从目标看，民主社会主义的目标和社会主义的目标是相似的。但从手段看，民主社会主义提倡使用的手段与社会主义不同。按共产主义者的说法，这使民主社会主义的革命无法彻底。<br />
