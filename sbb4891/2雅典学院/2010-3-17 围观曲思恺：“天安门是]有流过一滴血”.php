																											<div id="postmessage_521362" class="t_msgfont">　　　　　　　　　　　　纪念三位遇难的大学生<br />
　　　　　　　　　　　　――“六四”十三周年祭<br />
　　　　　　　　　　　　　　・丁子霖・<br />
<br />
　　　　今年“六四”周年，我谨以此文祭奠三位遇难的大学生。他们是：<br />
　　　　北京农业大学园艺系硕士研究生戴金平<br />
　　　　中国人民大学苏联东欧研究所双学位生程仁兴<br />
　　　　清华大学无线电电子学系硕士研究生周德平<br />
<br />
　　　　他们倒在了天安门广场<br />
<br />
　　这三位中的两位，戴金平和程仁兴，都是被戒严部队射杀于天安门广场的。前者倒在毛泽东纪念堂附近，后者倒在天安门前的旗杆下。而在我找到他们之前，我一直无法肯定地回答天安门广场有没有死人这个被问了不知有多少遍的问题。<br />
<br />
　　这不禁使我想起１９９６年的一件往事。那年１２月，中国国防部长迟浩田去美国访问。当他在美国国防大学讲演结束时，曾有该所大学的学生就８９年“六四”屠杀学生和市民一事提出质询。迟浩田竞在大庭广众之下以不负责任的外交谎言来回答人们对一件严肃政治事件的提问。<br />
<br />
　　他信誓旦旦地说：“我当时作为解放军总参谋长，我在这里可以负责的、认真的回答朋友们。天安门广场上没有死一个人，出问题就出在东西方向和南面方向，有一点问题。就是有点推推撞撞。”<br />
<br />
　　话说得竞是如此轻松！死了那么多人，竞被说成仅仅是“有一点问题”。但是，我当时无法肯定广场上究竟死人了没有？我心里想，政府坚持这样的说法也许作过调查，掌握有确实的证据吧。由于没有相反的证据，当时我只是就迟所说“就是有点推推撞撞”的谎言提出要同他当面对质。<br />
<br />
　　现在，这件事快过去六年了，我已知道当时至少有两名大学生死于天安门广场，而且其中一名就死在“共和国”的旗杆下。但我反倒不想再同那位党国大员作什么对质了，因为他不配！<br />
<br />
　　在中国，谎言成了堂而皇之的政府行为。政府大员们能把谎言撒到国际上去，他们在国内的作为也就可想而知了。但是，大员们的谎言对于国内的黎民百姓来说，就没有像在彬彬有礼的外交场合那样轻松了。它是一种灾难，一种可以把人们推向绝路、置于死地的灾难。这里，还是让我们来听一听那些在层层谎言覆盖下的无辜生灵的呻吟吧。<br />
<br />
　　　　什么叫生不如死？<br />
<br />
　　我要为之祭奠的三位死难者，他们都是农家子弟，都来自湖北农村，都出生于多子女家庭，又都是全家唯一受高等教育者。他们的兄弟姐妹多数务农，他们生前都肩负着改变全家贫困境遇的希翼和重托。但是，在十三年前的那个黑色周末，这三个家庭几乎在同一个时刻坠入了苦难的深渊。从此，他们年迈的父母年复一年地守望在阴暗潮湿的家门口，期盼着儿子的魂魄能回到自己的身边，更思想着自己能早日随儿子去到那不再有痛苦的世界。<br />
<br />
　　那是１９９５年的冬天，在一个没有阳光的日子里，一位大半辈子与泥土打交道的诚实农民，程仁兴的父亲，终于因贫病交加、不堪承受丧子之痛而离开了人世。临终前，他用尽仅有的一点力气呼喊着儿子的名字：“仁兴仔啊！阿爸来陪你了！”<br />
<br />
　　老人死后，程母经受不住接连的打击，几次想随丈夫去找自己的儿子。有一次，她趁家人不备，怀里揣着儿子的遗像，把一条绳索系在了自家的房梁上，决定随儿子而去，幸而被年仅十岁的孙儿及时发现。孩子苦苦哀求，用自己稚嫩的肩膀支撑着奶奶的身躯，足足有一个多小时才被大人赶来救下。<br />
<br />
　　什么叫生不如死？我想无须我再多说什么了。<br />
<br />
　　程仁兴是我所在学校的学生。８９年我儿子遇难后，我碾转于病塌之上。我从前来看望我的学生和同事那里得知，人民大学有７名大学生、研究生遇难。其中就有一位叫程仁兴的（当时人们把他的名字误为程红兴）。<br />
<br />
　　出于对同难者命运的关切，我曾托湖南的同事、朋友，学生了解死者及其亲属的情况。但由于死者档案已在“六四”后封存，也由于知情者慑于当局压力不敢向我提供有关线索，在很长的一段时间里我根本无法进一步了解死者及其亲属的情况（尤其是死者亲属的下落），也就只好暂且放下。十多年来，我一直心存歉疚。我曾作过无数次努力，然而，寻找线索总是在最后一刻断绝了，留下的是挥之不去的懊丧和无奈。<br />
<br />
　　大概是感动了上苍的缘故吧，终于在去年年底的一天，一个偶然的机会又重新点燃起了我已经失落多年的希望。那天我家里来了一位素不相识的客人，他询问起在我们已经掌握的死难者名单里有没有一个叫程仁兴的？他说如果我们还没有找到这位死者的家庭，他有可能向我们提供帮助。<br />
<br />
　　两天后，也就是２００２年的元旦，这位朋友果然给我送来了有关死者程仁兴及其家属的资料。一位与我们这个群体毫无关涉的朋友，又是在“六四”事件过去这么多年之后，竞还能如此同情和关心我们的群体，我还能用什么样的语言来表示对他的感谢呢！我想我能做的，就是即刻同程的家属取得联系。<br />
<br />
　　第二天我就按这位朋友提供的地址给程的父亲写了一封信。当然，我并不知道程的父亲已经在７年前去世了。我也不知道在过去的十多年里，由于地方行政区划的变动，原来的通信地址已不能再用了。但居然程的亲属收到了我的这封信。后来知道是当地同情程家遭遇的一些好心人几易其手，最后才把信送到程家的。<br />
<br />
　　从程家的回信中我得知：今年年初，风烛残年的程母自摔了一跤后便卧病不起，因无钱住院治疗，只能在家里承受煎熬。于是我立即给程家汇去了一笔救助款。十三年了，这是这家难属第一次得到海内外朋友的关心和帮助。当我收到程家寄回的捐款收据并获知老人已被送往医院治疗后，我连日来一直悬着的心才落到了实处。尽管，这并没有减轻我内心的那份沉重。我想，要是能早点同程家取得联系，也许这个家庭的景况会好一点，也许程的父亲还不至于那样匆匆地离开人世。<br />
<br />
　　　　他也是一位清华学子<br />
<br />
　　今年２月２２日，来华访问的布什总统去清华大学发表演讲并回答学生的提问。那天，我确实如我的学生、女记者高瑜在一篇文章里所说的那样，是流着眼泪听完布什总统讲演的。这倒不是因为总统的讲演有什么特别动人之处，而是我深感于中美两国制度之徊异。<br />
<br />
　　同样是人，为什么我们中国人就不能象美国人那样享有本该属于所有人的那份自由和权利呢？为什么人类历史已进入２１世纪的今天，还需要由一位外国总统不远万里来到中国的最高学府，给那些一向被认为是最优秀的才俊们宣讲人类的普世价值，而且所讲的不过是一些最普通的常识呢？<br />
<br />
　　再看看那些一个个经过精心挑选的清华学子，他们那傲慢中掺杂着自卑、倾羡中掺杂着嫉妒的神态，我不能不想起当年他们那位学长――清华无线电系硕士生周德平。我很想对那天有幸坐在讲演大厅里的青年朋友们说，当你们为清华出了那么多党政要员们而感到荣光的时候，是否也应该记住：早在１３年前，你们的那位学长就已经在以自己的行动实现今天布什总统所宣讲的一切进行奋斗了，而且他已为此付出了生命的代价。<br />
<br />
　　我――一个曾经在大学的课堂里和讲台上度过大半辈子的人――就是在这百感交集的思绪中听完布什总统演讲的。我想到了８９年的那场运动，想到了当年有那么多青年男女倒在血泊中，想到了今天仍然有很多中国人经受着各种苦难。我――一个曾经在８９年失去了儿子的母亲――为我们的民族有周德平这样的清华优秀学子而感到骄傲和安慰，也为今天出现在我眼前的那个场景而感到悲观和失望。<br />
<br />
　　说起清华，说起周德平，我的思绪不由得从济济一堂的讲演大厅转到湖北南部一个贫困的小村庄，那里曾经是养育清华学子周德平的地方。他已经从这块曾萌生童年梦想的土地上消失了，但他那年逾七旬的父母仍生活在原来的地方。自儿子遇难后，那年年月月伴随着两位老人的，只是无尽的凄苦和绝望。<br />
<br />
　　我是在６年前才找到周的亲属的。我自己无法去看望这两位老人，一直到去年冬天，才委托一位朋友顺道去看望了他们。朋友告诉我：<br />
<br />
　　周家所住两间平房是全村最破旧的，因年久失修，门窗都掉落了，寒风直往屋里灌。家里没有一件象样的家什，连仅有的几张板凳都已破败不堪。别人家看上彩电都已有好多年了，但周家连一台黑白电视机都还没有。周母患神经性面瘫，脸部扭曲，仅剩下几颗残齿的牙床整个裸露在外面。周父常年哮喘，身躯卷曲，也已失去劳动能力。这些年来我们给他们转去的救助捐款，仅够两位老人艰难度日而已。９８年长江闹大灾，周家颗粒无收，房子也被冲坏了。９９年周父重病垂危，被人用门板抬到了医院。如果不是及时给他们追加救助，景况可想而知。<br />
<br />
　　我常常想，如果他们的儿子不死，他们会活得这样凄惨吗？<br />
<br />
　　但是，就是对这样勉强维持着残生的老人，共产党的官员也还不肯放过他们。<br />
<br />
　　９８年我给周家寄去的捐款，居然被村上的共产党书记冒领了去，最后还是由我出面交涉才退回给了两位老人。<br />
<br />
　　去年秋天，周家再遭骚扰。这回是村干部带领镇上派出所的民警一行，５、６人开着吉普车到了周家。他们协迫两位老人“老实交待”接收北京汇款的情况，说是再要发现有北京来的汇款，他们就立即扣下。他们厉声恶语地威胁两位老人：今后不得擅自离村。出门要事先向他们“请假”，回家还要向他们汇报。如有外人来访，也要及时报告。这种强加的侮辱性规定，同毛泽东时代对待所谓“五类份子”的做法有什么两样！<br />
<br />
　　我常常想，同样都是人，甚至共处一片土地、同住一个村庄，为什么竞能如此不把别人当人来看待？！中国的所谓“改革开放”也已经有２０多个年头了，为什么还不能把那些侮辱人、作贱人、把人不当人的做法稍稍改变一下呢？为什么还不能稍稍打开一点门户，让中国的老百姓也能享有到一点在别人看来已经象穿衣吃饭那样普通的人权和人的尊严之类的东西呢？我想，这也许就是一位“化外之邦”的总统要不远万里来到中国“兜售”他那个为权贵们所不肖的“西方价值”的缘故吧。<br />
<br />
　　　　一个家庭就这样被毁了<br />
<br />
　　下面，我要说的是另一位“六四”的死难者――北京农业大学园艺系硕士生戴金平。那年他毕业在即，本来有一个很好的前程，家人也有了一个盼头。但是，转瞬之间这一切都化成了泡影。<br />
<br />
　　农大在“六四”事件中有一位研究生遇难，我是在“六四”后不久就知道的。但我不知道他的名字，也苦于查找不到线索。一直到１１年以后，即２０００年冬天，我才从难友苏冰娴女士那里得知死者的有关信息。那是苏女士的一位老同学，一位农大教授无意间向她透露的。之后，苏又设法得到了戴的亲属在湖北农村的地址。但是，还没有等到与这家难属取得联系，苏冰娴女士却突然离开了人世。<br />
<br />
　　苏的不幸去世犹如晴天霹雳，使我陷入了极度的悲痛之中。但是，当我一想到苏生前把这家难属交到我手上的情景，我立即意识到我必须马上去完成这件她生前未了的事情。于是，我没有等到办完苏的丧事就给这难属发出了查询的信件。<br />
<br />
　　几天后，我在忐忑不安等待中终于收到了这家难属的回信。但信中所述却令我难以淬读――想不到这又是一个苦难深重的家庭！信中说：<br />
<br />
　　自戴遇难后，这个家庭所有的希望落空了。随之而来的是，戴母因悲伤过度，常年疾病缠身，完全丧失了劳动能力。戴家本来属于湖北的贫困地区，而且是一个没有任何其他收入的纯农户，现在一下子陷入了困境。为生活所迫，戴父不得不只身远走他乡去了武汉，靠出卖苦力打工养家。<br />
<br />
　　但想不到在以后的几年里，这个不幸的家庭又连遭厄运。９５年，戴家的小儿子突患神经分裂症，家里又失去了一个劳动力。９８年，外出打工的戴父在武汉的汉正街无缘无故地被歹徒打死，末了还落得个状告无门的结局。<br />
<br />
　　一个家庭就这样转眼之间被毁了，只剩下了一个无依无靠，被疾病折磨得几乎难以支撑的老妇人。对于这样一位连遭劫难的农村老妇来说，还有什么比这更残酷的呢！<br />
<br />
　　　　让我们记住这些名字吧！<br />
<br />
　　这些年来，程仁兴、周德平、戴金平……这一个个仿佛仍活着的名字，常常萦绕在我的脑际，令我无法忘怀。而当我一想起这些名字背后一个个如此不幸和苦难的家庭，更让我寝食难安。我苦于不能经常给这些家庭写信，因为信件经常遭检查和没收；我也苦于不能在电话里把要说的话明白说出，因为我的电话是受监控的。我怕给他们带来新的伤害，但是，我又无法把他们放下。我给他们转送救助捐款，每一次都总是提心吊胆地盼着能早日收到他们的回条。因为，如果手续上出一点差错，或者捐款中途被截，我知道这对他们意味着什么。为此我不得不谨慎从事，仔细考虑每一个环节，转上好几道“弯”才把捐款安全地送到他们手里。<br />
<br />
　　这就是我和这些受难家庭时时刻刻都要面对的现实。它是那样的黑暗和险恶，那样的虚伪和冷酷，那样的令人憎恶。<br />
<br />
　　今年春节期间，我从电视屏幕上看到各级党政要员们一窝蜂地跑到边远地区去“访贫问苦”，口口声声说要关心“弱势群体”，心里不由得一阵恶心。今年三月，我又从两代会的电视报道中看到有那么多的人民代表慷慨陈词，口口声声说要关注“农村的贫困”，我又禁不住一阵恶心。现在，表演时节已经过去，供他们表演的场地也已收拾干净，还有谁能见到他们再去问一问农民的死活和疾苦！<br />
<br />
　　我看透了这个制度的虚伪和腐败，更看透了那些党国大员的假仁假义，他们整日价把所谓的“三讲”、“三个代表”挂在嘴上，但他们除了关心自己的权势和钱袋，有几个真正把老百姓的死活放在心上？一切都被他们“代表”去了，老百姓除了任由他们欺压和宰割，还敢说什么？还能说什么？<br />
<br />
　　让我们记住程仁兴、周德平、戴金平这些名字吧！<br />
<br />
　　他们是敢于把那些假仁假义的大员和代表们推到一边，用自己的行动、自己的语言站出来说话的人。他们带着家乡父老的嘱托，带着神圣的使命感，走出了家乡，又走出了校门。他们已经倒在了天安门广场上，倒在了“共和国”的旗杆下。我不知是否还有人想起他们，但我相信，他们的名字将被历史记录下来，作为一个时代的象征。<br />
<br />
□　寄自中国</div>

							
							
							
							
