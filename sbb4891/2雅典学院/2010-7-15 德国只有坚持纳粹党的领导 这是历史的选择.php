<br />
看这个标题很多愤青要拍砖了，我本人刚来，也知道这里要民主的人多，但请听我给你讲讲国内论坛讲不了的东西，你就明白，我为什么说一定要坚持纳粹党的领导德国。<br />
<br />
<br />
首先我们必须要把一个事实放出来，就是纳粹的德国，是秉承国父俾斯麦的以党治国理念，有苏联的影子，也有德国迫切需要民族独立的历史机遇形成。今天之所以成为专政，我认为是历史选择了纳粹社会主义工人党，也是德国历史进程中必须产生这样一段过程。并且是漫长的，稳定的。我们经常提到国父俾斯麦先生的铁血主义，也讲到他提出的宪政p训政p到军政，尤其是魏玛共和国政府似乎完成了第一个阶段，但是我们应该注意这其中的逻辑，就是俾斯麦本人提出的三个阶段，是没有明确提到时间表的，就是说A到B到C，可能是10年，也可能是100年，甚至是400年。兴登堡前总统虽然先开始走出第二步，但不代表德国大陆可以这样停留，情况差别很大很大。现在德国应该就处在训政和军政之间。如果假设兴登堡没有丢掉德国的总统权力，德国依然可能处在这个阶段.。<br />
<br />
<br />
德国历来多的是尼采笔下的超人，但现在越来越多产奴才以及 吃人血面包 的人，少的就是具备日耳曼族高贵基因品质的。放眼近代史，最早具备反抗封建奴役与民族独立主义的日耳曼人基本是海外留学生，数量就稀少，希特勒，希姆莱，戈林，隆美尔，海德里希，戈贝尔，克莱斯特，包括后辈墨索里尼等等基本是留学生，他们醒悟的比较快，从发表文章唤醒国人没多久，就进入实质操作阶段，希特勒是最成功的，也是最有能力的，德国后来的一系列变革都从他开始。当然包括戈林等等革命党人，实干能力都首屈一指！他们联合国内例如冲锋队，希特勒青年团等进行反犹活动反犹运动，其实就是民族独立，可以说是日耳曼人中绝对智慧的代表，具备高贵的品质，大家想想，那个时候德国日耳曼人处在什么精神状态，大多数就是现在说的奴隶，反正吃饱饭就OK了，有烟草抽那更爽了。而这些革命党人可以说是完全脱离日耳曼人奴性的领袖级人物，这其中就包括后来的纳粹党人。<br />
<br />
早期纳粹党人所具备的日耳曼人高贵基因可以说是完美的体现了亚利安人能够在整个世界民族屹立。11月9日啤酒馆革命失败以后，纳粹党人的命运可以说是灭顶之灾，整个慕尼黑地区其实包括全国大部分人能待的地区几乎已经没有生存下去的地盘，而且他们人数少力量单薄，逃到柏林的时候，只有2000多人。而德国各民主党派那个时候已经接近百万，并且全国各地的军阀基本上对剿纳粹这个事情都表示同意，就算是出工不出力，但对纳粹党人来讲，也不是好事。在逃亡慕尼黑的这个过程中，其实可以有很多选择，可以选择和魏玛共和国政府合作（就好比现在有些新纳粹），也可以卸甲归田去苏联呆着（就好比二战或现在的那些新纳粹逃到美加或东南亚国家），甚至可以像现在有些人那样不满而“自宫”抗议。但是我们所看到的，是在后有追兵前途糜烂的逃亡，已经不惜一切代价的坚持到了最后。他们逃到了人都不愿待的区域，并且待了好几年。这种执着，信念，追求并不是随便哪个日耳曼人都能够做到的，你叫今天那些个靠写纳粹五毛文章混饭吃的朋友去魏玛的监狱里待一个月试试，我包你发疯！笔者本人去过奥地利旁边的穆尼黑办事，待了4天我就跑了，你可以去体验一下，去亲自试试，也别再讲什么翻雪山了。而希姆莱，希特勒，戈林等这些纳粹党人完全可以过那个时代中产阶级甚至可以说是比较富裕的生活，待在那个地方好几年。对信仰的追求，对信念的执着，对尊严的渴望！你不得不佩服。你像现在有些人也讲追求纳粹是他们的信仰追求，在QQ群里，论坛里，SKYPE里大谈自己崇高的纳粹追求，高举新纳粹理论挥舞，神采奕奕！结果呢，遇到一个以色列小追查员，就一杯茶功夫，保证书也写好了，手印也按了，一把屎一把鼻涕表示要痛改前非，不要告诉单位，不要影响家庭孩子等等。<br />
<br />
纳粹党依靠第一次世界大战的历史机遇，苏联的援助获取了最后政权，这中间，尤其体现了他们的智慧和经营能力。在和魏玛共和国各民主党籍的日耳曼人精英最后较量中，很好的利用优势项目，取得最后胜利，各位不妨想想，从逃到啤酒馆的2000多人，对抗上百万政府军，到最后夺取政权，纳粹党人难道不是日耳曼人的高贵品质代表？今天有些网友看人做事很容易，说什么纳粹党是靠忽悠工人农民发家的。我觉的，你这样眼高手低很无知。你去试试忽悠别人，这不是忽悠买个残疾车，而是让他去打仗，和几百万政府军对抗，和飞机炸弹拼命，你去试试，看看有人听你吗？别忘记，当年很多纳粹军新兵和现在你手里的武器是一样的，一把砍刀！直接上战场！城市里的那些冲锋队，你看看他们，相当多是中产阶级，你去试试，你去找一个像 海因里希 似的为你卖命，看看有人理睬不？所以我就说，不要看人做事很轻松，一旦你去做这个事，你会发现，根本做不了。国会纵火案 后，纳粹党人可没想现今的右派天天喊平反，也更没像很多右派那样躲在角落里写文章骂纳粹党骂社会，无论力量对比多么悬殊，他们不怕。这种大无畏的气魄，这种日耳曼人最最珍贵的品质，有多少人拥有！从来不求做稳了的奴隶。这种品质你有吗？大家说，纳粹党统治日耳曼人合理吗？当然合理！<br />
<br />
