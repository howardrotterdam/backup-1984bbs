																											<div id="postmessage_418489" class="t_msgfont">迎着西伯利亚寒风而立的橡树――悼念反极权的伟大思想者索尔仁尼琴<br />
&nbsp; &nbsp;来源：观察 <br />
&nbsp; &nbsp; 遥想上世纪八十年代中期，中国知识界正处在高度亢奋之中，各类模仿而来的先锋文学蜂拥而至，就连王蒙也要写“意识流”小说；右派作家群的“大墙文学”洛阳纸贵，用苦难贴金变成知名作家的标志。恰是在这样的氛围中，我读到了内部出版的三大本《古拉格群岛》。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;我第一次读到索尔仁尼琴的《古拉格群岛》，震撼我的不是集中营的故事（因为中国人所经历的残忍远远超过了索氏的叙述），而是这位具有深邃宗教关怀的极权主义的叛逆者所表现出来的诚实和自省――无论是顺从还是反叛，极权主义的受害者在某种程度上都是极权制度的同谋。<br />
&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp;这是一部朴素得没有任何技巧和修饰的巨著，全无先锋文学的任何痕迹；如果说这部巨著还有什么技巧的话，那就是“写实、写实、再写实”，让我联想到西藏高原的大山，在高远的蓝天下，透亮的阳光让山峰耸立变得格外突兀，山上的巨石也裸露得峥嵘，山顶的积雪终年不化，山下的溪水冷透骨髓。正是在这样高度透明的写实中，世界知道了“古拉格”这个刺目的名字。作为共产极权的代名词，“古拉格”包含着二十世纪最极端的制度罪恶，是阶级灭绝、政治恐怖和人性堕落的象征，其沉重的份量，甚至超过作为种族灭绝代名词的“奥茨维辛”。所以，当这部巨著于1973年在西方出版之后，颠覆了左倾知识分子的红色情结，让苏联神话在西方破产。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;这是一部带有浓烈宗教精神的反思力作，书中的每一行都是苦难，而且是让人难以想象的绝对苦难，但作者没有夸张、放大，没有怨天尤人，更没有炫耀苦难和用苦难贴金，而是在对制度性苦难进行毫发毕现的描绘的同时，对苦难的根源作出了痛切而深刻的反省。这种反省，不仅是针对斯大林制度，也是针对受害者群体。邪恶在俄罗斯的畅通无阻，即使受害者群体不是残酷而卑鄙的，起码也是懦弱而愚昧的。看当时的苏联，人们不敢活在真实中，客观上成了斯大林主义的帮凶；看俄国的历史，俄罗斯变成共产苏联的过程，既是崇尚暴力革命和迷信恐怖手段的结果，也是企图建立地上天国的世俗狂妄的结果。当俄罗斯的大多数民众放弃托尔斯泰主义而选择列宁主义之时，俄罗斯变成苏联的命运就已经注定。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;也许，正是这种精神高度才让他踏上了流亡之路。在苏联的“解冻时期”，这位“劳改犯作家”受到过苏共党魁赫鲁晓夫赏识，但他并没有受宠若惊的喜悦和踏上受招安的红地毯，而是坚持与极权主义为敌。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;回头看中国改革时期的右派文学，不仅在揭露毛时代罪恶的力度上远逊于苏东作家（近几年出现的《夹边沟纪事》等劳改纪实作品，开始接近《古拉格群岛》的揭露），而且在反思的深度上更有天壤之别。在中国知识分子笔下，是喋喋不休的诉苦和把苦难的责任全部推给外在原因，是用苦难为自己贴金和自我美化，是“青春万岁”的理想主义赞歌和“两头真”的自我辩护，稍好一点儿的作品，至多是苦难中的第二种忠诚。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;在此意义上，一部《古拉格群岛》的思想重量，胜过中国揭露毛泽东时代罪恶的文字的总和。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;凡是由独裁制度转型为民主制度成功的国家，不但有持续的组织资源的积累，还要有持续不断的思想资源的积累，两者的相辅相成才会造就成型的民间反对运动。而中国社会的政治转型少有进展的原因之一，就是民间的思想资源远比其他转型各国贫乏，而且没有持续积累。苏东各国本身就深受西欧自由主义传统的影响，这种影响通过理论和文学艺术的传播一直没有中断。更由于五十年代的解冻，苏东各国的民间知识精英创造出大量反极权的经典作品，哈维尔给捷克总统的公开信所具有的精神深度，胜过中国的持不同政见者发表的所有宣言。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;改革开放三十年以来，深层的体制局限和知识人本身的精神残缺的综合作用，使具有强烈现实关怀的中国知识界弊端众生。清算毛时代的言论大都局限于推卸责任和用苦难贴金，鲜有深刻的负责的自我反思；倡导自由主义的言说大都陷于游移、浅薄和暧昧的窘境，激情有余而思想不足，打擦边球过多而直来直去太少。致力于传统的创造性转化的新儒家，无法理顺传统价值与现代自由主义之间的衔接关系，“传统内圣”根本开不出“现代之新外王”，不经意中成了官方爱国主义的文化脚注和理论借口；致力于反传统的西化思潮对自由主义的理解非常笼统和浅薄，也没找到适合中国国情的表述方式。中国文化思想界当时的现状是：启蒙者成长的思想环境，是被斗争哲学和语言暴力扫荡一空的文化沙漠，启蒙者本身就首先需要接受启蒙，怎么可能在一个急需广泛而深入的思想启蒙的大变革时代，发动融深刻的批判性和有远见的建设性为一体的启蒙运动！当基本的健全制度的常识和善待人性的常识在精英阶层中间还远未得以建立之时，怎么可能把这种常识普及到大众水平。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;从八十年代中期开始，在民众中填补共产信仰崩溃所留下的灵魂空白的，既不是被修正的中共意识形态，也不是知识界的人道派、新儒家或西化派，而是从八十年代中期就开始风靡的“气功热”、“武侠热”和“琼瑶热”，这三者都是中国传统文化的当代载体，恰好与文化界的寻根思潮相互激荡。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;八九运动就是在这样贫乏的思想启蒙和残缺的知识品格之下开始的，自由知识分子在运动中并没有表现出应有的思想魅力，更缺乏把启蒙思想转化为实际行动的胆量和能力。参与八九运动的大学生和市民，其轰轰烈烈的行动留下了丰厚的道义遗产，但是在精神遗产上，参加运动的知识精英们却没有留下什么值得骄傲的思想痕迹。亲历过这个伟大运动的知识精英，如果回头看看自己当时发表的言论，凡是诚实的反思者都会觉得脸红。知识精英为这个运动提供的思想资源与这个运动表现出的道义激情、动员规模、和平理性相比，简直太不相称。两者之间的巨大反差，甚至可以用渺小和伟大之比来形容。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;六四后，中共全力反对西化自由化的和平演变，高举爱国主义和弘扬传统文化，于是，准宗教的各类气功组织迅速扩张，王朔的小说和影视的风靡，以反讽喜剧的方式娱乐着政治高压的社会，帮助人们宣泄万马齐喑的压抑，在用嘲讽来颠覆中共意识形态合法性的同时，也具有着消解一切价值和理想的倾向，助长了虚无主义和玩世不恭的痞子态度。不幸的是，在王朔热风靡的大众传播中，前一种倾向日渐消失而后一种倾向却深入人心。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;其实，具有颠覆和虚无的双重色彩的王朔热，它们的迅速大众化是正统意识形态崩溃后的正常现象。在正义感严重受挫和政治激情得不到释放的无奈之中，人们转向强身健体和延年益寿的准宗教，转向纵欲享乐和对金钱的贪婪，也就水到渠成。抛开官方的普遍政治腐败对社会精神的致命毒化不谈，仅从民间的角度讲，知识精英没有资格苛责王朔们和迷恋通俗文化的大众。让王朔们对中国社会的灵魂空白和道德糜烂负主要责任，是极不公平的。每个人都有局限，王朔们所起到的颠覆作用已经足够，填补颠覆之后的精神废墟的主要责任，本应该由知识精英来承担。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;从整体精神氛围上看，对于民众精神的伤害之惨烈，无过于六四大屠杀之后的精英犬儒主义。知识精英的自私懦弱把平民推向枪口而自己逃向安全之地，热衷于为权贵私有化和权钱交易的腐败辩护的精英本身也已经权贵化，不可能关注受迫害群体的艰难抗争，更不可能为弱势群体的悲惨处境而仗义执言。即便是关注社会公正的自由主义左翼和号称具有坚定的平民立场的新老毛派，也决不会在现实行为中为受迫害者仗义执言。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;这就是具有中国特色的权力、金钱和知识之间的同谋，知识精英群体根本不可能成为社会良知的象征和填补灵魂空白的启蒙者。所以，王朔们的名字对平民阶层的巨大魅力，不仅证明了官方意识形态灌输的失败，也凸现了知识精英的启蒙思想和知识品格的贫困。与其没完没了地谴责社会精神的痞子化，不如拿出自省的勇气和负责的理性检讨知识精英自身的犬儒化。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;八十年代，看苏联异见者的作品；九十年代，看东欧异见者的文字；作为中国异见人士的我，不免脸红。从读沙哈罗夫、索尔仁尼琴、哈维尔、米奇尼克的时刻起，他们反抗极权体制的言行都堪称我的榜样，我对他们的钦佩之情和自卑之心从未消失过。他们的知识结构、政治视野、宗教情怀和自省精神所造就的灵魂深度，才是成就他们的伟大作品和伟大行动的内在动力。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;在他们的言行中，我读到的，不仅是极权的罪恶，更有受害者本身的反躬自省：我们每个人，不仅是极权主义的受害者，而且也是它的参与者和缔造者；今天我们反抗独裁体制的行动，不仅是良知和勇气的意图伦理，更是理性而负责的责任伦理。如何取得道义勇气、斗争策略和行动效果之间的平衡，以便让民间反对运动的效果最大化，应该是中国民间反对派最应该探讨的关键问题。以目前中国民间生态的状况看，健康的民间反对伦理的建立还需要很长的时间。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;索尔仁尼琴的文字之沉重，让供养生命的鲜血变成黑色的抗争，似乎血色的灵魂就是专门为释放一种黑色的忧郁而准备的。这种忧郁向我展示的，与其说是共产极权下异见者的勇气，不如说是思想者所能达到的精神高度；与其说是天才的华美文学，不如说是教徒的忏悔精神。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;一双完全裸露的眼睛，犹如残酷的真理，为自己的坦率而牺牲。凝视这双眼睛中的风景，我相信“石头缝中也能长出迎着西伯利亚寒风而立的橡树”。</div>

							
							
							
							
