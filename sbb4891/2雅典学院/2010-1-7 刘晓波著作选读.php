																											<div id="postmessage_418480" class="t_msgfont">迎风而立的王千源<br />
&nbsp; &nbsp;来源：观察 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;在由西藏危机引发的中西冲突中，中共政权及其愤青的愤怒似乎来自西方对中国的不公正的妖魔化。事实上，妖魔化不是来自外部，而是来自中国内部--独裁政府及爱国愤青的自我妖魔化，用不堪入目来形容，一点儿一也不过分。海内外爱国华人对王千源的文革式围剿，就是这种自我妖魔化的最新例证。<br />
&nbsp; &nbsp;在爱国主义早已变成中国的绝对政治正确的压力下，在五星红旗连成片的红海洋里，在仍然习惯于&quot;合群的自大&quot;的群体盲从中，甚至在愤青们野蛮的言行已经危及到家人的险境中，一个年仅二十岁的中国姑娘却玉树临风，向整个世界展示出新一代中国人的健全人格。<br />
&nbsp; &nbsp;这位值得尊敬的姑娘就是美国杜克大学一年级中国藉留学生王千源。遗憾的是，她的独立声音没有得到她的祖国的最低限度的尊重。<br />
<br />
&nbsp; &nbsp;人们常说，爱国主义是双刃剑。但在我看来，和平时期的独裁化爱国主义仅仅是单刃毒剑，它的锋芒来自极端的仇恨，它的闪亮来自极端的愚昧，她的杀伤力来自极端的野蛮，无论对外还是对内，它的主要手段是制造敌人，对外制造&quot;反华势力&quot;，对内制造&quot;汉奸&quot;。所以，无论是王千源的善意调解，还是金晶的理性爱国，皆被愤青们作为&quot;汉奸&quot;的铁证。<br />
&nbsp; &nbsp;极端爱国主义还是可以让人陷入群体性颠狂的春药，无视基本事实，泯灭是非善恶，罔顾普世价值，践踏基本人权。所以，海内外愤青们根本不管王千源站在对立双方的中间仅仅是为了促成对话交流的善意，不管王千源的&quot;不支持国家分裂，不愿意有意识针对任何国人&quot;的表白，更不尊重王千源的基本人权，对她发动了一场文革式的野蛮围剿。<br />
&nbsp; &nbsp;中共头号喉舌中央电视台网站4月17号在首页以《最丑陋的留学生》刊登了她的照片和视频，王千源的照片在中国各大网站上流传，有的照片经过处理后在额头上打上&quot;叛国贼&quot;字眼。王千源的名字、身份证号、联络电话等个人资料统统被公布在网上。有人更诅咒这位20岁的如花女子被汽油焚烧，甚至威胁她回国后将被碎尸万段。<br />
&nbsp; &nbsp;王千源的父母在青岛的公寓地址、姓名、工作等资讯也被上网，有人还在网上贴出王千源父母家门口被泼粪的照片。她父母的家已经被毁坏，家门口安装了摄像头，双亲只能在外面暂避。围剿殃及她父母的理由也很堂皇，王家出了这样的&quot;叛国贼&quot;，其父母自然难辞其咎。<br />
&nbsp; &nbsp;王千源毕业于青岛二中，既是该校的高才生，也是青岛的高才生，曾经是该校对外炫耀的资本。但现在，她瞬间变成爱国者的&quot;公敌&quot;，愤青们也把攻击的矛头指向她的母校，大骂学校教出这样的学生是一个失败。该校马上出来表态，坚决与她划清界线，不承认有王千源这样的学生，已经将她的毕业证书作废，还召开全校&quot;整风&quot;大会，加强&quot;爱国主义教育&quot;。该校教务处的李老师对记者表示：&quot;全校的师生都很恨她。&quot;<br />
&nbsp; &nbsp;经历过文革的国人都知道，政权号召、官媒点名、大众声讨和单位批判相结合，构成文革式大批判的基本模式。对王千源的围剿完全符合这种模式：有中共高官对达赖喇嘛的文革式指控为大背景，有外交部新闻发言人在三天内三次要求CNN道歉为示范，有中共头号电视喉舌央视为号召，有海内外愤青的群体性声讨、谩骂和恫吓，有她的母校与之划清界线。所以，把对王千源的声讨命名为&quot;文革式的围剿&quot;，一点儿也不冤枉。<br />
&nbsp; &nbsp;幸运的是，王千源人在美国并一举成名，可以利用现在的知名度进一步表达自己的观点，扩大她的言行在海内外的影响，让中国愤青看看什么叫独立清醒坚定的80后青年，也让西方看到中国的年轻人并非都是&quot;爱国红卫兵&quot;。<br />
&nbsp; &nbsp;在一连串的采访中，王千源谈到不同政见对于国家进步的重要作用，谈到自己对于&quot;出头&quot;的理解，谈到对西藏问题和宗教自由的看法，特别是她道出独裁爱国主义对中国年轻一代的深重危害。她说：指责她的人不是有意的，对方也是中共的受害者。在文革和中共的思想控制和愚民政策包围之下，大家都是受害者，再去迫害别人。<br />
&nbsp; &nbsp;正因为王千源认识到独裁制度对国家和国人的危害，她才能明确地区分出独裁爱国主义与民主爱国主义。她说：独裁将大家的手足都切掉了，将大家的思维都控制住之后，把我们的世界不断的缩少，这些才是真正的卖国。而反对的声音帮助中国进步，民主让国民变得更强大，让人民自己修养自己，思维自己。她还表示，好像中国是磐古开天时的一个蛋，是一个偶合来的、黑暗的蛋，你需要里面的力量往外膨胀，外面的壳不断地往里收，所以唯一出线的结果是蛋壳要裂。<br />
&nbsp; &nbsp;如果说，王千源出面敦促对立双方进行对话交流的行动，还仅仅体现了她不盲从的独立性，那么，她面对殃及家人的文革式围剿时的平静坚定，表现的就是迎风而立的成熟勇气。她平静地坚持自己的独立思考和自由思想，用法律手段捍卫自己的独立人格和自由表达。<br />
&nbsp; &nbsp;也许，此次西藏危机引发的爱国主义过于&quot;文革化&quot;，王千源的独立姿态才显得格外突兀，让我想起当年第一个挑战&quot;血统论&quot;的遇罗克；也许，发动反西方爱国狂飙的主体是80后一代，王千源的勇气理性成熟才显得鹤立鸡群，与国内80后一代中极为罕见的韩寒遥相呼应。<br />
&nbsp; &nbsp;观察中国国内九十年代到今天的历次爱国狂潮，每次颠狂在官权的严控下沉寂之后，&quot;愤青&quot;们都会因灰心丧气而发生分化，每次分化都可能让一部分&quot;愤青&quot;转化为&quot;汉奸&quot;。我相信，王千源那迎风而立的美丽姿态，将对中国的年轻人产生不可小视的感召力。当此次爱国颠狂平息下来，&quot;愤青&quot;群体必然还会分化出&quot;汉奸&quot;，那些抛弃专制爱国主义而认同民主爱国主义的年轻人。<br />
&nbsp; &nbsp;2008年4月22日于北京家中（《观察》2008年4月22日）</div>

							
							
							
							
