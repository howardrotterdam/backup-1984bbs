																											<div id="postmessage_418469" class="t_msgfont">开幕式-独裁美学的精华版<br />
&nbsp; &nbsp;来源：观察 <br />
<br />
&nbsp; &nbsp; 用当今中国最著名的御用导演包装现政权的最大政治Party――北京奥运开幕式，上下五千年和大场面，可以猜到；高科技声光和浓墨重彩，可以预期。但不可能有什么想象力和创造性，更不可能展示一个真实的中国形象。<br />
<br />
&nbsp; &nbsp;张艺谋说开幕式只有两个字：“浪漫”。央视主持人周涛说：“开幕式除了浪漫还是浪漫”。<br />
<br />
&nbsp; &nbsp;在我看来，张艺谋导演的开幕式，大都是自我重复，想象力匮乏的整体构思，几乎空白的人文底蕴，仅靠细节安排的高科技“小聪明”，不可能制造出真正的惊奇，也就不可能有“浪漫”。<br />
<br />
&nbsp; &nbsp;外在的包装，依然是科技声光、浓墨重彩、人海战术和整齐划一，只有宏大的场面和名信片般的漂亮，却没有美。<br />
<br />
<br />
&nbsp; &nbsp;内在的含蕴，还是老掉牙的传统元素和最时髦的当代政治，如同用现代手段重新包装昔日的坟墓，只有用金钱堆积起来的陈腐，却没有“魂”。<br />
<br />
&nbsp; &nbsp;四大发明、丝绸之路、戏曲，瓷器、礼乐、茶叶、太极、兵俑、书法、绘画、孔子……传统大杂烩聚焦为权力意志，一个反复变幻的“和”，是专门表演给主席台正中看的。<br />
<br />
&nbsp; &nbsp;开幕式的解说词经过央视喉舌的朗诵腔演绎，如同事前录播的字正腔圆，四六句成了解说词的主体，配上华丽的形容词，直逼中国最早的献媚文体――汉大赋。<br />
<br />
&nbsp; &nbsp;世界上有无数种“媚俗”，最恶心的“媚俗”当然是“媚权”。如此“媚权”的开幕式，还好意思自称为“浪漫”！<br />
<br />
&nbsp; &nbsp;开幕式，像张艺谋以往的大片、大制作一样，也是通过高科技来制作张艺谋擅长的矫情画面，展示了一种“唯漂亮主义”的悦目效果，是对观众的智商和审美能力的侮辱。<br />
<br />
&nbsp; &nbsp;独裁权力，政治上野蛮，道德上邪恶，审美上病态。独裁美学对“大一统”有着病态的嗜好，最喜欢大数字、大场面、大人群和大话，动不动就举行盛大的仪式，大规模集会和大型团体操，盛大的阅兵仪式和宏大的节庆晚会，一向是独裁美学的拿手好戏。无数演员千百次地重复着同样的动作，喊着同样的口号，就是没有人、没有心、没有爱、没有美。<br />
<br />
&nbsp; &nbsp;张艺谋，曾被誉为大陆的先锋导演，他在上世纪九十年代拍的影片多次被中共电影衙门封杀过。但进入二十一世纪，张艺谋似乎如梦方醒，先是专拍“真善美”的主旋律，继而专拍商业大片和导演大型商业秀。<br />
<br />
&nbsp; &nbsp;早期的张艺谋专拍小人物，如《红高粱》、《秋菊打官司》和《活着》，却能拍出大气象。现在的张艺谋专拍大人物，如《英雄》、《十面埋伏》、《满城尽带黄金甲》，却部部沦落为小风景，场面越大，气魄越小，那种浓墨重彩的暴力美学，表现着灰烬般的观念。<br />
<br />
&nbsp; &nbsp;西方人来中国，大都要登长城；张艺谋向世界推介中国文化，制造的却是虚假的“文化长城”。如果说，雅典奥运闭幕式上的8分钟中国红，更像中南海红墙的影子；那么，北京奥运开幕式上的一个半小时，已经变成赤裸裸的“大国崛起”与“和谐社会”了。<br />
<br />
&nbsp; &nbsp;众所周知，从担任《黄土地》的摄影到导演一系列有影响的影片，张艺谋是靠小投入且倍受争议、甚至被官方封杀的先锋电影而名扬海内外的，时至今日，他执导的名片《活着》，仍然无法在大陆公映。而后极权中国的怪诞就在于，昔日受打压的逆子变成了今日的小康盛世的宠儿，张艺谋既是国际知名的大导演，也是接到最多政府大定单的首席官方导演。为了张艺谋大片创造一个个票房神话，他得到了电影衙门的精心庇护，他的每部大片都会享受放映档起的优惠。<br />
<br />
&nbsp; &nbsp;现在，只要张导开工，无论干什么弱智的制作，皆有大资金投入。比如，《英雄》耗资近2.5亿人民币，《十面埋伏》耗资2.2亿人民币，《满城尽带黄金甲》耗资3.6亿元人民币；实景歌剧《印象刘三姐》耗资2亿人民币，在韩国改编版耗资60亿韩元，折合4000万人民币；意大利实景古典歌剧《图兰朵》耗资1500万美元，折合1.3亿人民币；还有芭蕾舞剧《大红灯笼高高挂》和申奥广告片等……资金投入总额应在10亿元以上。此次奥运开幕式的资金投入，也是前无古人的1亿-2亿美金。<br />
<br />
&nbsp; &nbsp;故而，今日中国电影的张艺谋模式被概括为八大：用“大投入、大阵容、大制作、大营销”来制造“大电影、大事件、大快乐、大票房”。<br />
<br />
&nbsp; &nbsp;从被主旋律排斥的导演到制作一系列精神麻醉剂的当红戏子，张艺谋从叛逆到驯顺的电影生涯，非常典型地浓缩了六四后中共统治术的变化和中国文化人的角色嬗变，标志着后极权政治对各类精英的成功收买。<br />
<br />
&nbsp; &nbsp;从《英雄》到《十面埋伏》的武打大片，从桂林山水秀到奥运开幕式的大型制作，张艺谋用越来越多的资金投入，制作出越来越多的文化垃圾，而只有这类文化垃圾，才越来越符合当下主旋律的审美情趣――重大政治庆典宣传片和大型招商广告片，春节晚会的舞台和MTV式画面――定型化和程式化的灌输和麻醉。这类主旋律所构建的视、听方式，已经把大众文化变成官方主旋律的附庸，显示出独裁权力制造愚民效应的高超技巧。<br />
<br />
&nbsp; &nbsp;如果说，赵本山小品，是愚民的主旋律粗俗版；那么，张艺谋的大制作，不过是小品精神的精华版而已。因为，无论投入多少金钱，动员多少人力，聘请多少大腕，在骨子里，后极权的独裁美学最欣赏的是小品精神。<br />
<br />
&nbsp; &nbsp;2008年8月9日于北京家中</div>

							
							
							
							
