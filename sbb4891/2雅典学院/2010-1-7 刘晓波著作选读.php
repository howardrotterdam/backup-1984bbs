&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp;自江泽民上台以来，在官方语言中，他是“江核心”；而在民间舆论中，他却是“政治戏子”。看看大陆民间流行的讽刺性“新民谣”，拿江寻开心的段子最多。<br />
<br />
&nbsp; &nbsp;在如此蔑视“江核心”的舆论环境中，如果江不是利令智昏，在两年前的十六大上，他就应该象其他六常委那样下台。这样，对他的形象来说，也许还能起到一两拨千斤的作用，多少挽回一点声誉，赢得些许体面。<br />
<br />
&nbsp; &nbsp;然而，他就是利令智昏，想学邓小平的先例，硬要顶风上，以普通党员之身连任军头，但他与邓小平的权威差之千里，邓的垂枪听政都要招致骂声，而既无邓的资历和威望、又无邓的魄力和智慧的江，却硬撑着连任军头，境内外从未停止过的挺胡贬江的舆论潮，实乃江的咎由自取。<br />
<br />
<br />
&nbsp; &nbsp;江留任两年，既是自我作贱，又是以丑角为胡温当托儿。看看十六大以后的国内外舆论，凡是到江的，全是盼他尽早下台。比如，二届、三届中共全会，每一次都有一边倒地促江下台的舆论。以至于，由于盼江下台的党心民意过于强烈，社会舆论对十六大以来中国事务的评价，出现了极端情绪化的偏向：垂枪听政的江泽民，不但变成了胡温新政的垃圾桶，而且殃及进入高层的江系人马――几乎所有的坏事都是江系干的，而所有的好事都是胡温干的；即便胡温干不好的事，也是由于江系捣乱。<br />
<br />
&nbsp; &nbsp;如此一来，不论是否喜欢胡温，反江的党内外力量都要向胡温聚积；进入十六大政治局中的江派人马，也都跟着江沾上一身腥。逐渐地，即便是靠江的提携进入中共核心层的人，也不愿被舆论看成江的傀儡，更不想替江背骂名，自然也希望江早日下台。<br />
<br />
&nbsp; &nbsp;大概，唯一不希望江下台的，只有江氏家族。<br />
<br />
&nbsp; &nbsp;也许，江泽民终于悟出了这一点；更大的可能，是江在坚持了两年之后，实在顶不住党心民意的压力，不得不交出军权。但，现在交权，在党心民意那里，已经体面全无，剩下的评价，也就只有权争的失败者。曾庆红没能填补副军头的空缺，似乎已验证了江的失败。<br />
<br />
&nbsp; &nbsp;尽管，全会关于江辞职的公报，通篇是不着边际的赞美，开了中共历史上“未盖棺，先论定”的先河；尽管，全会闭幕时，江、胡共同见与会代表，胡作了满嘴抹蜜的开场白，对江的恭维亲切得有点离谱，似乎他现在集党政军三权于一身，不是邓小平钦定的结果，而是江禅让给他的。但两年来，他的总书记和国家主席之尊，却屡屡跟在江身后出席军方活动，那种屈辱的滋味，胡心里最清楚。当然，胡也对与江明争暗斗的辛苦，最有体会。<br />
<br />
&nbsp; &nbsp;江泽民不愧被称为“政治戏子”，离开他眷恋的权力宝座时，也想把自己的谢幕表演成“鞠躬尽瘁、死而后已”的高潮，不但写下了无私为党为国为民的请辞信，而且在向代表们发表谢幕讲话时，那种前所未有的高亢语调，肃然把自己的请辞当成伟大的壮举。但他又不得不对新主人胡锦涛作肉麻奉承。因为，对江而言，这最后的谢幕，只为了至关重要的一点：保证江氏家族的利益安全。<br />
<br />
&nbsp; &nbsp;江泽民下台了，十五年大权在握，除了邓南巡时被吓出一身冷汗外，可谓出尽风头，但作为一个大国的最有权势的政客，却平庸得让人懒得评价，勉强说一句，也就是给个“独裁戏子”的俗名。<br />
<br />
&nbsp; &nbsp;胡锦涛圆满了，集党政军三权于一身，但胡的权力象江一样，来自邓的钦定，他也可能是江的翻版，不敢超出跛足改革的邓模式。而不敢超越邓模式的党魁，肯定无大作为，平庸到下台。<br />
<br />
&nbsp; &nbsp;只是，江丑角谢幕了，再无人替胡顶雷了。<br />
<br />
&nbsp; &nbsp;胡如何收拾邓、江留下的残局，只能拭目以待。<br />
