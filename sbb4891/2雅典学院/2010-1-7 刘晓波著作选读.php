																											<div id="postmessage_418459" class="t_msgfont">温家宝的道德狂妄<br />
<br />
来源：观察 <br />
&nbsp; &nbsp; 最近，中共总理温家宝在美国演讲和接受采访，大谈道德。<br />
&nbsp; &nbsp;谈到毒奶粉事件，温家宝说：“一个企业家身上应该流着道德的血液。只有把看得见的企业技术、产品和管理，以及背后引导他们并受他们影响的理念、道德和责任，两者加在一起才能构成经济和企业的DNA。”他还说：“中央政府在得知奶粉问题后第一时间公开。”<br />
&nbsp; &nbsp;而事实上，毒奶粉危机的爆发和蔓延，显然是黑心资本与黑心权力的联手投毒，要论责任，我以为政府的监管渎职和新闻操控的责任更大，如果没有黑心权力的纵容和庇护，如果没有奥运期间严格的新闻管制，三鹿毒奶粉绝不会长时间、大面积作恶。但温家宝只把道德利刃对准商界，却丝毫不提中央政府的责任，而且用“第一时间公开”为中央政府辩解，难道不是政客的道德败血症吗？<br />
&nbsp; &nbsp;谈到作为总理的自我期许，温家宝说：“我希望留给后人两点精神遗产：第一，当遇到灾难时不要退缩，要勇于面对，并且带领人民去克服，这需要坚定、勇气和信心；第二，一个政府，除了对人民的负责、服务、献身和廉洁以外，不应该有任何特权。一切权力属于人民，一切权力都要为了人民。我希望在我离开人世之后，人们能记住这位总理，确实是按照这两点来做的。”<br />
&nbsp; &nbsp;温家宝“希望留给后人两点精神遗产”，可以概括为知难而上的“大无畏”和行使权力的“大公无私”。但如此“大无畏”的总理，却不敢正面回答赵紫阳和六四的问题，也不敢诚实地回答中国的人权问题，非但不敢，还公然说谎：“自由言论和媒体自由报道是得到了承诺的。”如此“大公无私”的总理，却从不提及关于他妻儿暴富的负面传闻。<br />
&nbsp; &nbsp;虽然，温家宝以“平民总理”自居，但作为寡头独裁政权的巨头之一，他不可能具有真正的权力谦卑，所以，他谈道德的口气一点儿也不平民，而仍然是一副居高临下的教训腔调。故而，他的道德论调所表达的，与其说是所谓“平民总理”的高尚自律，不如说是独裁寡头的道德训诫，那种骨子里的权力狂妄想掩饰也掩饰不了。<br />
&nbsp; &nbsp;在中国的制度和文化中，权力崇拜带来当权者就是道德圣人的狂妄，所有执政者都喜欢唱道德高调，已经成为一以贯之的传统，正如孔子所言：“君子之德风，小人之德草，草上之风必偃”。但从古至今的现实所显示的景观恰恰相反：最爱讲道德的中国从来没有过言行一致的传统。帝制时代，二千多年的“仁、义、礼、智、信”的高调，也没有唱出几个真正的清官和明君。民国时期，孙中山的天下为公和蒋介石的新生活运动，带来的却是极端腐败无能的国民党政权。中共执政后，毛泽东唱了二十七年“全心全意为人民服务”的高调，唱出的却是人与人之间的残酷斗争；后毛时代，从邓小平的“五讲四美”到江泽民的“以德治国”再到胡锦涛的“八荣八耻”，带来的却是官场的腐败和公德的沦落。<br />
&nbsp; &nbsp;面对中共官场的普遍腐败和社会公德的普遍沦落，中共执政集团不思从制度改革上入手，不从根治权贵集团普遍腐败上着眼，反而一次次地祭起了钦定道德标准的陈腐旗帜，以为靠最高当权者的道德召唤就能澄清玉宇。但这种由当权者发布道德训诫的陋习，与其说是为了从思想意识上道德上遏制腐败，不如说只是为了突出权势者多么有道德；与其说是为大陆社会的道德建设指出了方向，不如说是又一次倒行逆施，只能使目前“缺德”的现状愈演愈烈。现在，温家宝的大无畏和大公无私的高调，也无法遏制全社会道德的急遽败坏。<br />
&nbsp; &nbsp;温家宝的道德训诫所奉行的统治逻辑极为陈腐，与现代人类文明毫无共同之处，倒是让我不断地回到二十四史所记载的几千年帝制时代，历代帝王们为民众钦定思想权威和道德标准的圣谕传统，不仅其中记载的中国古代礼仪之繁复，令我不敢想象，更令我不敢相信的是，古今一脉，表面上是儒家“道统”制约皇权“法统”，实质上是皇权法统主宰儒家道统。礼仪道德之标准皆由官方发布，常常就是皇帝本人的圣谕。比如秦始皇的“焚书坑儒”、汉武帝的“独尊儒术”、唐太宗的“贞观礼仪一百三十八篇”、宋太祖的“礼仪诏”，特别是朱元璋统治下的明代初期，皇权管制之宽之严类似于毛泽东时代的大陆，已经深入到每个人的吃穿住行生老病死婚嫁交友的所有细节，其表现便是繁复的各类以“圣谕”的形式颁布的礼仪。<br />
&nbsp; &nbsp;以皇家，特别是以皇帝一人为象征的最高皇权是衡量一切的唯一标准，它颁布各类禁令和制定各种制度，皆由皇帝确定并昭示天下。比如，从皇帝到庶民的男人，可以分为十几个等级，皇帝、皇太子、亲王、公、伯、一至九品、平民，平民又分为士农工商；女人从皇后一直到妓女，其等级也有几十层，层层排序，各守本位，不得逾越。其等级森严包括衣食住行、婚丧嫁娶、节庆宴请等所有生活的细节，真是“一览众山小”，一权天下威，一人天下父。比如，明太祖洪武二十年对民房的定制：庶民庐舍，不过三间，五架，不许用斗拱、饰彩色；洪武三年对服饰定制：乐妓衣饰明角冠，皂褙子，不许与民妻同。在农人与商人之间也规定了严格的服饰界限：农人衣饰由纱、绢、布做，而商人不能用纱做衣服。农家有一人从商，全家都不能用纱制衣。此类规定即是法律，违者必受刑罚，重者可以因穿衣而丧生。生活于这些礼仪中的人还有什么个人和自由可言，说“专制到细胞和灵魂”真是恰如其分。<br />
&nbsp; &nbsp;在如此森严的等级制之下，中国人已经彻底丧失了被剥夺自由的耻辱感，每个人从未把自己当作人，统治者没有，被统治者也没有。所谓“人”，在中国古代的哲学中仅仅是区别于兽禽的称呼，而一进入社会就没有了人，而只有主子、臣子、奴才、妻、妾、妃。所谓历代之改制，也从未触及专制等级制和钦定道德的传统，而仅仅是为了使之更完善而已。奇怪的是，在统治者如此高高在上地俯视人甚至完全无视人的制度下，居然无人对此制度提出过根本的质疑，无人把人本身的权利作为其道德的基础。<br />
&nbsp; &nbsp;真正把皇权法统和儒家道统完全合一的独裁时代，则开始于中共执政的1949年，中共的每一代核心都既代表法统的最高权力又代表道统的最高权威，从毛思想到邓理论至江学说，莫不如此。所以，古代的钦定道德的手法之荒谬程度，远不如当代独裁统治的舆论造势之手法，毛泽东时代乃是这类荒谬的极致，其余韵至今犹在。<br />
&nbsp; &nbsp;在人类历史上，制度、法律、思想和道德的建设、改进、创新和完善，皆是自发演进长期积累的结果，而不是某一个完美的统治者、思想权威或道德教主创造并颁布的。文明越进步，政治权力对思想和道德的强制干涉就越不具有合法性。任何统治者无权把某种思想或某种道德钦定为唯一权威，无权利用手中的政治权力向社会进行强制性灌输，无权为维持其统治权力而强制建立一元化的思想或道德的秩序。相反，政治权力必须在思想上和道德上采取中立态度，让思想和道德在自由的多元化格局中，通过自发的竞争进行交流、对话、包容、渗透、融合，才会使一个社会具有健全的道德秩序和不断的思想创新，才会使人类在优胜劣汰的公平自由竞争中保持生生不息的创造活力。<br />
&nbsp; &nbsp;这一切，在二十一世纪的今天，已经成为起码的人文常识，而只有少数顽固坚持专制统治的独裁者及特权集团，才会自以为无所不知无所不能，才会相信自己的绝对正确，狂妄到为社会为民众钦定思想权威和道德标准的蒙昧程度。这种陈腐而野蛮的权力狂妄，既是对人的自由及尊严的践踏，也是对人的智慧的蔑视。这样的统治者除了绝对相信自己的能力之外，把其他人皆当作无独立能力的依附者──无思考能力和选择能力的弱智者或残疾人，把当权者代替众生思考和选择视为赐福于民，把剥夺人的自由的思想权利和选择权利视为理所当然。中国的统治者最爱说：“此为礼也，用以教民”，他们甚至狂妄到可笑而弱智的程度，比如，明王朝的缔造者朱元璋创建的贯穿整个明朝的一项制度，居然是每个月月初都要发布指导百姓怎样生活的圣谕，由顺天府的头儿带领宛平县和大兴县的县令入宫领旨，然后在金水桥南头交给十位有头有脸的乡绅耆老。由于月月都要宣旨，圣谕的内容便高度重复，久而久之，聆听圣谕的耆老们就不耐烦了，索性花钱，一边贿赂宣旨的官员，一边雇佣了一些街头痞子，给他们制一套体面的行头，让他们代为听旨。宣读圣旨的官员收了好处，自然心知肚明，默认了此种欺君大罪。如此，耆老们避免了浪费时间的麻烦，官员和痞子得到了银子，三者共同在光天化日之下上演着欺君之秀，严肃地假装宣旨，恭敬地假装聆听，只有皇帝一人变成了白痴，被他的臣子和臣民玩弄于股掌之间。<br />
&nbsp; &nbsp;不幸的是，今日的中国人仍然在这样的狂妄而弱智的统治之下，思想和道德皆以统治者或统治者钦定的圣人为权威标准。这种“年年讲月月讲”的传统非但没有绝迹，反而被中共政权创造性地发展，变成了“天天讲时时讲，”毛泽东的最高指示、邓小平的一个中心两个基本点、江泽民的三个代表、胡锦涛的亲民路线，在其各自执政的时代皆是百姓每天必须聆听圣谕。一会儿是发布“道德纲要”，一会儿通过“建设精神文明决议”，一会儿又有“保先运动”，居高临下地频频教导老百姓如何遵守道德常识。这些“纲要”、“决议”和“运动”，居然把百姓当作幼儿园的孩子，要求百姓“尊老爱幼、男女平等、邻里团结、勤俭持家、助人为乐、遵纪守法、诚实守信……”等等。我想，那些被中宣部频频找来开会的官员们和每天被要求学习党魁训令的百姓们，其心情与明代的宫廷太监、大户乡绅、街头痞子完全一样，共同上演一出统治者装模作样地发号施令，被统治者装模作样地洗耳恭听的作秀肥皂剧。<br />
&nbsp; &nbsp;狂妄是所有独裁者的道德癌症，越是有所作为的独裁者就越狂妄，不用提外国，仅列出中国的秦始皇、汉武帝、唐太宗、明太祖、康熙乾隆、毛泽东……足矣！这样的政治强人也具有把癌细胞强制扩散到全社会的超强能力。尽管现在的中国已经告别了独裁强人的时代，但是并没有摆脱独裁制度的统治逻辑──所有的专制社会在道德上皆是野蛮的虚伪的堕落的，所谓中国为几千年礼仪之邦，不过是长期的意识形态强制灌输形成的虚假套话。鲁迅说“礼教吃人”，点出了礼仪之邦的野蛮性；陈寅恪说中国的礼仪制度“大抵仅为纸上之空文”，道破了礼仪之邦的虚伪性；野蛮而虚伪的礼仪之邦在道德上必是堕落的，因为布道者恰恰是最大的不讲道德者。当你不遵守他们颁布的道德礼仪之时，比如提出与统治者不同的政见，他们才不会只用道德说教来说服你，而是要用监狱甚至肉体灭绝来彻底取消你说话的权利和机会，正如古人违反了三从四德之时，他们不会只用儒家经典来劝说，而是要动用法家的严刑峻法来惩治。换言之，他们不仅用暴力恐怖吃人的肉体，更是用欺骗吃掉人性、人格、尊严和灵魂。<br />
中国，从古至今，以独裁权力为核心的等级制和礼仪道德，覆盖之广泛、统治之严酷、虚伪之透顶，实为举世罕见，甚至连以宗教立国的民族亦难比拟。由此，可以解释为何中国之独裁等级制得以几千年不衰，成为世界上最长寿的政体。<br />
&nbsp; &nbsp;当今的大陆仍然沿袭着传统帝制统治逻辑，执政集团既是权威的布道者，又是最大的道德腐败者和伪善者，政治腐败所导致的全社会的缺德现状，绝非统治者依靠最不道德的绝对权力发布什么道德训诫所能改变的。非但不能改变，反而只能加速全社会的道德荒漠化。<br />
&nbsp; &nbsp;2008年10月5日于北京家中</div>

							
							
							
							
