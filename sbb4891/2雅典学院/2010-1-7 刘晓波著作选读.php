																											<div id="postmessage_418474" class="t_msgfont">从野草到荒原―“2008年度当代汉语贡献奖”答谢辞<br />
&nbsp; &nbsp;来源：观察 <br />
<br />
&nbsp; &nbsp; 6月2日，距六四十九周年祭日仅两天，距汶川大地震二十一天。<br />
<br />
&nbsp; &nbsp;就在这个日子，北京当代汉语研究所发布公告，把“2008年度当代汉语贡献奖”授予了我，显然是基于我与六四之间的血肉关系。<br />
<br />
&nbsp; &nbsp;如此用心，让我感动。<br />
<br />
&nbsp; &nbsp;这，应该个好消息，但这个日子，之于我，无论如何也无以为乐。<br />
<br />
<br />
&nbsp; &nbsp;十九年前，那场举世震惊的大屠杀，让青春的激情扑到在坦克的履带下，让自由的理想喋血在黎明前的暗夜中。<br />
<br />
&nbsp; &nbsp;二十一天前，举世关注的汶川大地震，最令人悲愤的是坚固的衙门大楼与脆弱的中小学校舍之间的鲜明对比，最为揪心的惨剧是大量中小学变成废墟，成千上万的孩子葬身于豆腐渣工程。<br />
<br />
&nbsp; &nbsp;只是瞬间，那么多曾经鲜活的生命，或变成坦克履带下血肉模糊的一团，或变成废墟旁堆积起来的尸体。他们还未真正开始，生活已经结束；他们还没有梦想过，就失去了未来。<br />
<br />
&nbsp; &nbsp;十九年前，年轻学子死于惨烈的制度性人祸；二十一天前，更年幼的孩子死于天灾中的人祸。而一个无视青春和孩子的民族，即便能够建造出摩天大厦，也是在用高耸入云的崛起来装饰地狱；抗得住八级地震的衙门大楼，也是用钢筋水泥的坚固来自掘坟墓。<br />
<br />
&nbsp; &nbsp;是的，我没有丝毫兴奋，反而陷于更深的悲哀。作为大屠杀的幸存者，十九年来，尽管我努力抗争，试图活出尊严、做到合格，以无愧于年轻的亡灵。然而，冥冥之眼的俯视下，我仍然活在耻辱中。<br />
<br />
&nbsp; &nbsp;这耻辱，不是来自独裁权力的恐怖政治，十几年来如影随形的警察并不构成对勇气的挑战。当一个政权要动用国家机器来对付一个手无寸铁的知识人时，这个政权早已在根基上腐烂，蛮横不过是权力虚弱的表情。<br />
<br />
&nbsp; &nbsp;这耻辱，也不是来自党的喉舌延续数天的“歌功颂德”和“感恩戴德”的大合唱。当政府本职变成“皇恩浩荡”或凸显伟光正的资本之时，这主旋律早已变味为新民谣的笑料，只能奏出独裁者的黄昏。<br />
<br />
&nbsp; &nbsp;这耻辱，更不是来自社会的冷漠、知识界的犬儒或隔着大洋的道德高调。当御用智囊们论证前所未有的盛世和大国崛起之时，不过是精英们的故作深刻；当民众沉浸在金钱的追逐中或陶醉在小康的幸福中，不过是沉默大多数的硬作狂欢；当在恐怖下长期失语而在远离后突然高亢，不过是有了安全保单后的失态。<br />
<br />
&nbsp; &nbsp;这耻辱，来自我内心深处的挣扎。骄狂之光，恰恰来自内在阴暗。如同鲁迅笔下的野草，裸露在艾略特的荒原上。鲁迅的《野草》所表达的绝望，如同现代汉语写作的墓志铭。艾略特的《荒原》所吟唱的颓废，如同现代文明的挽歌。<br />
<br />
&nbsp; &nbsp;从一株野草的脆弱到无边荒原的广袤，从墓志铭到挽歌，除了羞愧，再无其他。回头看一眼自己的写作，至今仍然刻有成长于文化沙漠的烙印，也不时地流露出文革式的尾巴，太多的时评仅是过眼烟云，不管互联网上堆积起多少点击率，到头来还是逃不脱被扫进垃圾筒的命运。<br />
<br />
&nbsp; &nbsp;这样的汉语写作，如何当得起“华文世界第一健笔”！又如何当得起“贡献”二字！<br />
<br />
&nbsp; &nbsp;而关于死亡，我能说的、写的、做的，无论如何，决不会多于亡灵临终前的一瞥，也决不会多于天安门母亲守护遗像的白发。这俯视所带来的震撼，不啻于对我这个幸存者的道德审判。这白发所召唤的明天，就是对我这个幸存者的永远激励。<br />
<br />
&nbsp; &nbsp;凝视年轻的遗像，抚摸苍老的白发，我没有勇气把这个奖当作一束百合，献于没有墓碑的亡灵前。欲哭无泪的眼中，含着十九年也不剜不去的忏悔，我甚至不知道，用赎罪的谦卑能否换来良心的安顿。<br />
<br />
&nbsp; &nbsp;一株野草，晃动荒原的记忆，干枯的记忆铺出了一条深渊边的小径，突如其来的灵魂洞开，让石头的飞翔有了方向。<br />
<br />
&nbsp; &nbsp;这个奖，我接受并答谢，与其说把她作为赞美来接受，不如说把她作为警示来收藏。时刻提醒我，至少在年近半百之时，你之于汉语写作，还不配“贡献”二字。时刻激励我，至少在有生之年，你还要加倍地用心去写作，以配得上六四亡灵用生命书写的悲壮诗篇。<br />
<br />
&nbsp; &nbsp;野草，需要来自荒原的救赎。<br />
<br />
&nbsp; &nbsp;我的汉语写作，需要来自坟墓的诉说。<br />
<br />
&nbsp; &nbsp;2008年6月3日于北京家中（《观察》2008年6月3日）</div>

							
							
							
							
