																											<div id="postmessage_418456" class="t_msgfont">嗜血和失血并存的时代<br />
&nbsp; &nbsp;来源：观察 <br />
<br />
&nbsp; &nbsp; 以批判理论闻名的法兰克福学派，其最受关注的思想原创是对大众文化及文化工业的批判，已经被视为大众文化研究的理论基础，甚至有人断言，不读法兰克福学派的文化批判理论，就无资格谈论工业时代的大众文化。<br />
<br />
&nbsp; &nbsp;在法兰克福学派的文化理论中，霍克海默的《现代艺术和大众文化》又被视为奠基之作。他认为，真正的艺术创作是人类精神的探险，既要具有超现实的精神品质或乌托邦性质，又要具有批判锋芒。它们引起人们的不安、痛苦、怀疑、反抗，唤起人类对自由的记忆。它们不是苦难时代的安慰，而是享乐时代的痉挛。从艺术形式的角度讲，真正的艺术应该是高度个人化的、无法重复的。<br />
<br />
&nbsp; &nbsp;与真正的艺术相反，工业社会的大众文化不过是精神快餐和乐感消费，迎合着物质高度丰富的享乐时代，适应着工具化模式化的工业生产方式，使人的精神变得平面化或单面化，丧失了对真正艺术的欣赏能力。艺术不再是个人的独创，而是文化产业的批量制造，依赖于先进的技术和大工业模式。文化消费者只能接受大批量的标准化的文化产品。所以，现代资本主义文化的大众性、普及性与艺术创造的个人性和批判性毫无关系。换言之，资本主义制度自发地利用大众文化进行一体化的操纵。大众文化受到商品经济、市场交换的支配，变成了模式化的日常消费品。大众文化所培养的受众是一群趣味雷同、粗糙、低劣的同质人。<br />
<br />
&nbsp; &nbsp;霍克海默说：“今天，幸福的笑容都带着狂热的面具，唯有狂热者忧郁的脸还保留着希望。”（P217）<br />
<br />
<br />
&nbsp; &nbsp;技术创造的魔天大楼驱逐了上帝的天堂，技术的进步导致了人类心灵的普遍退化，灵魂的饥饿比胃的饥饿更残酷。故而，霍克海默说：“爱像饥饿一样是残酷的。”<br />
<br />
&nbsp; &nbsp;法兰克福学派的大众文化批判是极为激进的，凡是受到大众欢迎的文化产品，都是将导致人们的顺从，因而都被作为批判对象，甚至包括象陀思朵也夫斯基的小说、斯特拉文斯基的音乐以及一些存在主义的作品，都因其在某一方面或局部的妥协而受到阿多诺等人的批判。他们看重先锋艺术对现代社会的挑战，那种以荒诞形式表达的反叛和背离，直接指向无所不在的、无孔不入的、无形的操控和剥夺。比如贝克特的荒诞剧、布莱希特的间离效果，特别是卡夫卡的小说，在法兰克福学派看来就是反叛艺术的典范。卡夫卡以其极端的孤独和冷静，开掘出现代人被压抑被扭曲的存在境遇，现代社会的极权性质在《审判》、《城堡》等作品中得到了最具深度的象征性揭示。<br />
<br />
&nbsp; &nbsp;启蒙主义的理想催生了现代社会背离其最初的发展目标，走向了个体自由、主体自治、多元发展的反面，先锋艺术对现代社会的挑战也最终被资本主义的商品化和市场化所同化，变成同质性的大众文化的一部分，迎合市场和大众趣味的媚俗代替了独立的不妥协的批判，平庸代替了尖锐，蛋糕代替了毒药，对标新立异的病态追求代替天才的独创，新奇和病态成为了一种新的大众时尚，成为一种模仿性的复制、拷贝，丧失了终极价值的关怀和对痛苦的记忆。快餐式的生活方式已经渗透进文化的每一环节，及时享乐，短期行为，明星崇拜，追逐时髦，一切都是瞬间的、复制的、易拉罐式的，解一时之渴，弃之若垃圾。人们在抛弃神启的形而上学的审美趣味的同时，并没有真正地摆脱形而上学的操纵，正如霍克海默所说：“口腔糖并不消灭形而上学，而就是形而上学。”<br />
<br />
&nbsp; &nbsp;由这种大众的物质享受和文化消费构成的单面人是现代社会的主体，他们的世俗欲望使现代的消费社会患上了致命的癌症――一种喜气洋洋的灾难。富裕的生活、充分的享受、趣味的平均化、影视图象和大众明星添满了人们的闲暇……这一切都在窒息着人们的反思能力、怀疑精神和反抗冲动，使人的精神世界普遍地苍白化。不愿意独立思考，不愿意选择冒险的生活，对时尚和流行的盲目顺从，把现代社会变成了由可口可乐、流行音乐和肥皂剧组成的广场，平庸是它的唯一品质。在这种富裕的疾病中，人类失去了提升自己的生命质量、追求超越价值的冲动，失去了以怀疑和批判为起点的创造性和想象力。浮士德的精神探险变成了中产阶级的附庸风雅，堂。诘珂德的喜剧失去了内在的悲怆情调，耶稣的殉难精神再也不是生命的典范。<br />
<br />
&nbsp; &nbsp;在极权主义盛行的战后，人类的智者和良知的痛苦，被一种对具体的物质目标的疯狂追逐所代替。卡夫卡之后，再也没有文学。仅仅为了商业的利益和经济的实惠，那些政治掮客宁愿闭上眼睛，无视极权政权对基本人权的践踏和剥夺。<br />
<br />
&nbsp; &nbsp;对于在战后的富足中长大的一代人来说，苦难逐渐变成遥远的天方夜谭，至多引起几声感叹和廉价的眼泪。宗教的关怀、哲学的批判、艺术的叛逆以及骑士时代和灾难时代的种种英雄壮举，统统被喜气洋洋的享乐所吞没。现代人没有了幻想，而只有得到一台法拉利跑车的渺小心愿；不再被神的灵光照耀，只想仰望麦当娜漂亮的脸蛋和修长的大腿；不能容忍饥饿和瘟疫，却心安理得地接受各种名牌的阉割；再没有谦卑和敬畏，更谈不上为自己的堕落而忏悔和赎罪，而只有轻浮的高傲、廉价的悲伤、不负责任的放纵。失去了神的世界和不期待上帝的拯救，人世的黑暗和人的堕落就全无意义。<br />
<br />
&nbsp; &nbsp;启蒙运动的最大成就、同时也是最大遗患，就是以数字的方法审判人的思维，数字化成为人类生存的最基本条件。所有不能计算的、不能被数字化处理的，都被排除在人的行为之外。在这个数字化的世界中，最悲惨的就是那些无法数字化的人的存在本身。信仰、灵魂、爱、痛苦，在本性上是抗拒数字化的，但在无力抗拒的环境中，只能扭曲自己，俯首贴耳了，成为工具性的存在。在此意义上，数字与权力是同义的，其哲学形式就是各种类型的实证主义。理性成为万能的工具，理性化的人成了制造一切工具的工具。所以，正是这种理性之光中有一种阴冷和淡漠，而这恰恰是现代野蛮或蒙昧的种子。人在支配自然的同时，人对人的支配也在同步增长。人在多大程度上征服了自然，也就在多大程度上驯服了人本身。对自然的征服是以人的奴化为前提、为代价的，现代极权主义把这种奴役推向了极端。<br />
<br />
&nbsp; &nbsp;今天的中国，虽然离开了极权时代而进入了后极权时代，但奴役人的制度并没有完全离开；权力的暴虐性有所减弱，但权力的专横依旧；计划经济已被迫弃，但离现代资本主义仍然遥远；意识形态操控不再有效，但利益操控却无孔不入；革命文化已经远去，但大众文化变成新的麻醉剂，为骄横的权力带上一层温柔的面具，使奴才们可以向权力撒娇。<br />
<br />
&nbsp; &nbsp;本来，中国人就是功利的、浅薄的、冷酷的，现今的一切向钱看更把中国变成了只有“经济人”的畸形社会，一切人与人之间的关系都被实用和计算所主宰，没有共同承担苦难的心心相印，只是共同消费享乐的友谊。没有不计功利的爱，只有互为工具的利用。大家通常只在餐桌上嘻嘻哈哈，酒足饭饱后就形同路人。卡拉OK成了人们交际的主要空间，人与人之间的友谊变成了一种消谴，一种琐碎而甜得发腻的闲暇，友谊也像商品一样供人消费，甚至昔日的苦难也变成一种消费和闲暇的点缀。那些所谓的知青聚会和返乡寻根，至多是一种暴发户对自己贫乏过去的补偿，他们想衣锦还乡，是以加倍的炫耀向社会展示苦难的诱人美丽，一种类似丰乳霜的功能性装饰。<br />
<br />
&nbsp; &nbsp;一边是贪婪的血盆大口，一边是苍白的灵魂。<br />
<br />
&nbsp; &nbsp;当代中国正处在一个嗜血和失血并存的时代，我们的血液不再有血色，红只是一种生理现实，而不具有任何灵魂的色素。<br />
<br />
&nbsp; &nbsp;1998年10月17日于大连教养院<br />
<br />
&nbsp; &nbsp;2008年10月16日整理于北京家中<br />
<br />
&nbsp; &nbsp;（《观察》2008年10月16日）</div>

							
							
							
							
