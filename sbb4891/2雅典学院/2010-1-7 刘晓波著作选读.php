																											<div id="postmessage_418465" class="t_msgfont">超越始于恐惧<br />
&nbsp; &nbsp;来源：观察 <br />
<br />
&nbsp; &nbsp; 也许，世界上再没有比恐惧更复杂、更深刻的人生体验了。恐惧能够使人升华，在精神上走向崇高；也可以使人退缩，在精神上走向卑下；恐惧能够生发出拼搏的意志和力量，也可以使人走向沉沦和枯萎；恐惧感能够激发出卓越智慧、惊人勇气和超拔人格，也可以把人变成麻木的石头和猥琐的懦夫；恐惧，既包含着陌生、惊奇，也包含着痛苦、绝望。致命的恐惧，往往使人无所畏惧，构成生命的转折点。尽管痛苦、尽管挣扎、尽管毛骨悚然，然而，人无法现实地摆脱恐惧。<br />
<br />
&nbsp; &nbsp;在世俗的层面上，对强权和暴力、对贪婪和阴谋、对歧视和残酷、对饥饿和贫困的恐惧；在终极的关注中，对生，对死，对爱，对分裂、有限、短暂，对一切未知和不可知的……恐惧，成就了理性和信仰，也成就了人和人类文明。<br />
<br />
&nbsp; &nbsp;人类能够人为地制造无孔不入的社会性恐惧，也有能力通过制度创新摆脱这种恐惧。但是，后天的社会性恐惧能够摆脱，先天的本体性恐惧却无法完全摆脱。恐怖的独裁社会可以被免于恐怖的自由社会代替，但生命的衰老、灵魂的孤寂和痛苦却无法替代。<br />
<br />
&nbsp; &nbsp;科学再发达、财富再增加、社会再完善，人的本体性恐惧和痛苦也无法摆脱。故而，人需要精神家园，也就需要宗教和上帝！如果人类真有一天摆脱了恐惧，那么人也许就变成了另一种生物；如果一个民族缺乏恐惧感，那么这个民族就很难具有抗争精神；如果一个人没有恐惧，那他就会裹足不前。恐惧为生命的迸发提供动力和契机。它要求人抛弃一切等待和左顾右盼，去孤注一掷、去冒险、去将全部生命的活力调动起来。<br />
<br />
<br />
&nbsp; &nbsp;冒险是人类成就一切伟大事业的内在本能，而冒险在心理上和生理上的反应，既伴有惊奇，也必然伴有恐惧。毋宁说，惊奇往往就在恐惧之中，热爱冒险就是热爱恐惧。恐惧是邪恶的――当你屈从于恐惧之时；恐惧也是神奇的――当你不是逃避而是直面并决意战胜恐惧之时，生命的内在张力就会达到最大值，引导生命从事具有创造性的历险生活。对未知的未来和必死的结局的正视，将使人更积极地抓住今天和从过去汲取营养。<br />
<br />
&nbsp; &nbsp;如果没有人对恐惧的本能狂爱，莎士比亚笔下的那个野心勃勃的刽子手麦克白每分每秒都在忍受恐惧煎熬的场面，就不会直到今天还震撼人心。在那个平静的夜晚，半夜的敲门声使麦克白夫妇的紧张达到了最高度；如果没有恐惧，奥尼尔笔下的那个杀人如麻的琼斯皇被恐惧逼入绝境的场面，也不会产生巨大的舞台感染力。有有形的恐惧，也有无形的恐惧；有对外在威逼的恐惧，也有对内在自我的恐惧。无论从哪个角度看，恐惧的无法逃避恰好说明了恐惧是人的本能性反应，是人与宇宙之间的相互关系的重要纽带之一，是人与人之间的相互交往的基本方式之一。安稳的现实生活，需要冒险的刺激来摆脱空虚无聊，以至于没有危险也要人为地制造危险。<br />
<br />
&nbsp; &nbsp;不仅邪恶令人恐惧，即便是爱也令人恐惧。爱能造就一切，也能毁灭一切。假如你真爱一个人，你对他(她)的情感中一定伴有恐惧。母子之间、情人之间、夫妇之间，通过爱的方式而进行的残酷搏斗，发生在日常生活的时时刻刻。这种搏斗往往是最折磨人的，甚至亲人的一个表情都会有毛骨悚然的威慑效果。卡夫卡在《致父亲的信》中，详尽地描述了他对父亲的恐惧心理。一般人想来，这种恐惧应该是对生命的压抑和扼杀，但这种父子之间的心理搏斗却成就了一位创造力非凡的作家。<br />
<br />
&nbsp; &nbsp;敬仰，几乎是以恐惧为情感基础的，“敬畏感”便是对这种情感的准确表达。当人们站在肃穆的教堂中，面对耶稣受难的十字架，向往着上帝的天堂之时，难道那受难的鲜血不使人全身颤抖吗?难道毫无保留的忏悔不需要克服自我恐惧吗?上帝的仁慈、博爱、拯救决不是无条件的，被拯救者的心理恐惧是得到拯救的前提。在此意义上，形而上的信仰始于形而下的恐惧。<br />
<br />
&nbsp; &nbsp;在恐惧中，人的心理开始倾斜，人的生命开始了剧烈的运动，绷得紧紧的神经之弦，随时可能断裂。因此，伴随着对恐惧的热爱而来的，是一种相反的情绪体验：摆脱或克服恐惧。一旦现实的克服不可能，从人自身的生命中便会自然而然地生长出一种必要的心理补偿：想象的思辨的超现实、超时空的性质，为人在恐惧中获得心理上的平衡、精神上的自慰提供了最佳机能。于是，就有了与“理念”相伴随的“天堂”。有多少种哲学上的形而上学体系，就有多少种“乌托邦”。<br />
<br />
&nbsp; &nbsp;大凡形而上学都要在为宇宙规定一个本源之时，为人类寻找到一个最终的归宿，因为，宇宙的统一性在逻辑上要求有社会的统一性与之对应。所以，形而上学在宇宙观上、认识论上的乐观主义必然导致社会历史观上的乌托邦主义。它使人获得了一种虚幻的而又是必要的心理补偿。既然现实的人生总是分裂的、有限的、短暂的、痛苦的、残酷的，既然上帝创造人就是制造苦难的现世和幸福的来世，既然我们根本无法摆脱人在现实中的悲剧性宿命，那么就让我们在精神上去超越吧，在幻想中去认识、领悟和把握那个主宰万物的实体吧。领悟了统一就超越了分裂，领悟了无限与永恒就超越了有限和短暂，领悟了上帝就超越了现实和人自身，进入了“天堂”。当人与宇宙(时空)融为一体之时，所有的痛苦和恐惧都会消失。在这时，人是平静的、飘然的、幸福的，就像庄子哲学中的处子真人，更像那些历遭劫难而得到神之启示的宗教先知。相信那条简陋方舟吧，洪水定要退去，而绿色的橄榄枝，一定会出现在阳光普照之中。<br />
<br />
&nbsp; &nbsp;然而，如果一个人相信凭借自己的虔信最终能够达到人身合一的境界，那么这样的信徒仍然是狂妄的。人与神之间的鸿沟永远不会填平，无论信神多久，也无论信的多虔诚，人也无法摆脱原罪，忏悔与赎罪是信徒得意取悦神的主要方式。<br />
<br />
&nbsp; &nbsp;神在天上，人在地下，这才是宗教信仰中的神与人的关系。无论人如何努力，也无法改变这种关系。仰望是信仰的唯一姿态，而平视是信仰的坟墓。<br />
<br />
&nbsp; &nbsp;无论人神合一的时刻在精神上能够重复多少次，但在现实中却只有一次――死亡。基督教的教义说，当一个人躺在坟墓中的时候，他才走完了人世间的苦难历程；当一个人安详地闭上双目、停止呼吸之时，他才得到了彻底的解脱，聆听着来自天堂的呼唤。在这个意义上，形而上学的解脱很类似自杀，在走投无路的情况下悬梁自尽与在解释不了人的悲剧时的虚构，具有相同的性质。形而上学是精神上的自杀，是无肉体痛苦的消失。<br />
<br />
&nbsp; &nbsp;人啊，对你相信和崇拜的东西一定要备加小心。而我更欣赏虚幻信仰崩溃后的绝处逢生，欣赏那种面对废墟的乐观抗争。<br />
<br />
&nbsp; &nbsp;2008年9月13日于北京家中</div>

							
							
							
							
