																											<div id="postmessage_853363" class="t_msgfont">解放军五年藏区平叛艰苦岁月 <br />
<br />
2008年12月11日 08:10 兵器知识<br />
<br />
近年来国际反华势力利用其在西方的话语优势，宣扬达赖为代表的藏独势力一向主张的“和平”和“非暴力”，而了解西藏历史的人都知道这完全是颠倒黑白。应许多读者的要求，本刊特别邀请长期研究西藏平叛和中印边界问题的国防大学教授徐焰少将，撰写此文，介绍当年以达赖为首的西藏上层反动势力发动暴力叛乱的过程，以及解放军平叛作战的艰苦和所创造的辉煌业绩。<br />
<br />
　　文／国防大学教授、专业技术少将&nbsp;&nbsp;徐 焰<br />
<br />
　　1956年至1961年，政教合一的藏族上层集团曾发动过血腥的武装叛乱，最后被人民解放军和汉藏群众以艰苦的战斗平息。当时的参叛者包括受蒙蔽和裹胁的众多藏民和喇嘛，其骨干却是美国中央情报局和英印当局训练的康巴叛匪和藏军，并利用美国空投武器和特殊地形进行了长期“游击战”。解放军在交通极其不便、人烟稀少的雪域高原通过五年的艰苦平叛，终于获得了辉煌胜利，并由此获得了高原作战中使用各类武器的特殊经验。<br />
<br />
　　藏区人均枪支为全国之最，叛匪组织能力却很低下<br />
<br />
　　藏传佛教素来强调“戒杀”，近代西藏的僧俗上层集团却同外国勾结，大量输入武器装备寺院、土司院落和藏军。全国解放时西藏人口仅110万，加上川、滇、甘、青四省藏区总计藏族人口不过200万，整个藏区有枪却近10万支，20万喇嘛中许多人也有武器。如算上大量腰刀等冷兵器，藏区贵族真可谓“武装到了牙齿”。那些口喊慈悲、手捻佛珠的农奴主用的又是人头骨碗、人皮鼓和少女的腿骨号，其野蛮和摧残人权真是近代世界史上所罕见！<br />
<br />
　　1950年秋解放军开始向西藏进军，于1951年达成了和平解放的十七条协议。解放军入藏后，中央对其上层集团采取了长期等待的政策，争取其和平改革，几年内不改变农奴制度，也未收缴武器。反动僧俗势力看到先进文明的进入对其黑暗统治形成冲击，便在境外支持下发动并逐步扩大叛乱。1956年３月，川西康区的理塘叛乱打响了全国藏区叛乱的第一枪，此后云南迪庆、甘南和青海藏区的头人和反动喇嘛也率众围攻当地政府和驻军，解放军成都军区、兰州军区被迫平叛。康巴和青海叛匪逃入西藏后，得到噶厦当局收容，从1958年起不断袭击解放军驻藏部队。1959年３月拉萨发生暴乱，标志着局部叛乱发展到全藏区。<br />
<br />
　　藏区参叛人员主要分四大类：土司头人胁迫煽动的从叛藏民、武装喇嘛、藏军、康巴叛匪。前两类人数虽多却基本属于乌合之众。各土司、寺院之间又矛盾重重，还因争夺美国空投的卢比（印度货币当时在西藏通用）和武器经常火并。原由英印训练的藏军虽有一定的军事知识且有步、炮武器，却携家带口保命心理严重，许多人同解放军接触较多也了解共产党的政策，一遇打击和喊话多数人便缴械投降。最凶悍的是康巴叛匪，长期生活于汉藏交界地带，有械斗传统且有部分骨干受过国民党川军训练。康巴叛乱受打击后有7000余名匪徒骨干窜入西藏，美国中央情报局从中挑选部分人到关岛和本土科罗拉多州建立“康巴游击队营地”，由达赖的长兄当才活佛担任翻译进行训练，随后将受训者空投回藏作为叛乱骨干。<br />
<br />
　　笔者曾询问指挥过平叛作战的一些老将领，他们认为叛乱骨干单兵作战能力大多很强，使用的美制M1半自动步枪还优于我军制式武器。叛匪野外生存能力强，随身携带高热量的糌粑酥油可维持一个月食用（解放军携带粮食行军只能维持一星期），一般在野外蒙上皮衣即可睡眠，又因经常打猎枪法较准，子弹打完后用皮带夹石子投射也能伤人，还擅长腰刀格斗。当时解放军干部因多数是战争年代出身，作战经验丰富，战士却多系服役不久的义务兵，内地士兵在生理上还不适应高原条件。许多当事者称，平叛作战“真比红军爬雪山还苦”。除发扬“老西藏精神”外，解放军最大的优势在于组织协同能力强。<br />
<br />
　　军事集团的效率主要不在单兵素质，而在于整体力量协同。叛众由头人、活佛靠宗教和民族情绪煽动纠集而成，虽有美国空投的电信器材，也建立不起有效的指挥体系，作战时得手一拥而上，失败则四散奔逃。1958年秋冬山南泽当的解放军一个连被上千叛匪围攻，夜间院墙曾突然被美制ＴＮＴ炸药炸出大缺口，叛首叫喊着冲入，叛众却拖拖拉拉许久才跟上，结果解放军已调来机枪和迫击炮以火力封闭了缺口。解放军小部队外出遇伏击时，如迅速组织好环形防御，叛匪数量虽多也“啃不动”。叛众围攻时往往东边冲西边停，南边打北边无动静，小分队集中火力就能将其各个打退。当时叛匪虽以达赖作为精神支柱，作风极为野蛮，打起仗却多是“一盘散沙”。众多叛乱分子还沿袭宗教风俗和古老习惯，认为沾染血迹会弄脏身上的护身符，神佛就不能保佑，因而对负伤出血的同伙不加救护，任其自生自灭，结果负伤的叛乱分子除被解放军俘虏给予救治外，多数都因伤口恶化死亡。<br />
<br />
　　几十年来，西方公开媒体总是引述达赖的谣言，夸张地宣称1959年春“拉萨起义”时“中国驻藏军队有30万，在拉萨有５万军队”，以渲染所谓“镇压严酷”。其实了解军事常识和西藏地理条件的人都会知道，依靠川藏、青藏两条砂土路面的高原公路运输，中国在藏至多能供养几万军队。当时美国中央情报局就计算，西藏可以动员10万“游击队”，按反游击战的比例至少需要30万正规军才能对付，中国在当地却只能维持几万驻军，打20年也不可能平定。随后无情的事实却是，解放军除了原驻藏的六个团，仅仅增援了第54军和兰州军区第11师，最多投入兵力不过６万人，至1961年底便彻底消灭了叛匪，其它四省藏区的叛乱在此前也全部平定。平叛斗争能很快取得胜利，最根本的原因是共产党和人民政府以民主改革争取到多数藏族群众的支持。此外，解放军的战斗素质远远优于叛乱武装，步兵、炮兵、航空兵等兵种都有效使用了自身装备并密切协同，这是获胜的主要军事因素。<br />
<br />
　　航空兵精神威慑起初有效，实弹轰炸最终打散叛匪 <br />
<br />
　　青藏高原面积达200多万平方千米，山路难行，解放军以有限兵力入藏后长期只能“守点保线”，叛乱发生后一些孤立的县城遭受围攻时陆路增援困难，解放军刚组建不久的空军便有效发挥了支援地面部队的作用。<br />
<br />
　　1956年春康巴叛乱发生后。一些县城受到包围，甚至无法出城到河边取水。军委决定调动空军唯一的杜－４轰炸机团前往支援。杜－４是苏联仿照美国二战轰炸战略日本本土的超级空中堡垒“Ｂ－29”生产出的重型轰炸机，载弹９吨，最大航速558千米／小时，最大航程5100千米，留空时间长达10小时以上。虽然杜－４这种螺旋桨轰炸机在现代战争中突防能力已显不足，却在藏区平叛中充当了空中主角。<br />
<br />
　　康巴叛乱发生之初，中央仍对参叛头人进行劝说争取，因而要求空军既要设法解围又不要投实弹造成杀伤。面对这一难题，杜－４轰炸机只好采取震慑方式，到达指定空域后在地面聚集的叛众头顶低空俯冲，并投撒传单。绝大多数参叛者遇到从未见过的伴有巨大轰鸣声的“铁鸟”，一时吓得各自窜逃，守城人员才得以出城取水。为防止叛匪夜间再聚集攻城，杜－４还通宵盘旋，不时投下照明弹，使叛众误以为是“天灯”而不敢靠近。1957年末，青海许多土司头人和活佛图谋举行暴动，兰州军区闻讯后派一个连用几辆卡车将百余名心怀不轨的上层人物“请”到西宁机场，让其“参观”飞行表演。空军先以两架苏制拉－11活塞式战斗机对事先布置好的汽油桶进行低空扫射，接着用三架杜－２轰炸机投下几枚250千克级炸弹，一时靶区烈焰飞腾、大地震颤。据在场的空军人员回忆：喇嘛、活佛们都是很迷信的，对于飞机形象地将其称做“神鹰”，真感到解放军是“天兵天将”了。当时就有人经不住这种场合瘫倒在地，甚至有人尿了一裤子，还有人高呼：“共产党万岁！毛主席万岁！”真是丑态百出。随后这些惊魂未定的家伙被请去座谈，个个争先发言，表示拥护政府和维护民族团结，“那个热闹劲就别提了”。<br />
<br />
　　飞机的震慑虽起到一些作用，青海藏区反动上层最终受阶级本性驱使，仍于1958年春发动叛乱。起初解放军飞机只投撒传单和进行侦察，叛乱头子气焰又嚣张起来，甚至向部众蛊惑称只要有活佛念咒就能“刀枪不入”，渐渐又不怕飞机。于是，解放军在青海对格尔木附近的盐湖进行了平整，又开辟了玉树机场，作为杜－２轰炸机起降的基地并以实弹攻击。该机系苏联卫国战争期间生产的中型轰炸机，最大载弹量３吨，航速520千米/小时，航程2000千米。该机对机场要求不高，却因活动半径较小难以覆盖藏区。起初杜－２采用低空扫射，后发现经美国训练后空投的藏族特务组织排子枪对空射击，且懂得取提前量，汪自诚机组返航时就发现机身被击穿二十几个弹洞，因此各机组出击便要保持一定高度。自1959年以后，美国中央情报局曾用Ｂ－17和Ｃ－130空投了一些12.7毫米高射机枪，叛匪却因不大会操作和缺乏弹药而未能有效使用。不过杜－２因服役较久易出现故障，如周庭彦机组执行侦察任务时因机械故障迫降，遭地面叛匪疯狂围攻。机组成员着陆时多被撞成重伤，周庭彦只持一支54式手枪顽强抵抗，却无法对付四面蜂涌来的叛匪，全机组都被大刀砍杀。地面救援部队赶到时，发现烈士们都被割得肢体不全，惨状令人发指！<br />
<br />
　　藏区几年空中平叛作战的实践证明，杜－４轰炸机是最有效的装备。该机虽服役已久，却有４台发动机，即使有一两台发动机失灵仍可平安返航，在整个平叛期间全无损失。该型机载弹量大，机上安装10门23毫米口径的航空机炮，能进行七八个小时的盘旋扫射，名符其实成为“空中堡垒”。<br />
<br />
　　在平叛作战中，地面部队在高原难以携带重炮，叛乱分子经常依托石质的坚固寺庙顽抗，摧毁这类据点就要靠投掷重磅航弹。徒步追剿部队难以随身携带一星期以上的补给，需要大量空投。在茫茫草原追击叛乱武装又需要空中引导。解放军航空兵在平叛期间连续出动，不仅完成了空投和引导任务，还以持续的轰炸扫射使大股叛乱武装白天多不敢集中活动。经历过国内革命战争和抗美援朝战争的许多老战士都说：“过去是我们‘头上没帽子’老挨炸，在西藏倒是让叛匪尝了我们有制空权的滋味了！”<br />
<br />
　　藏区平叛最关键一仗是1959年３月的拉萨战斗，解放军炮兵发挥了火力骨干的作用。因西藏地形条件特殊，平叛时西藏军区只有第308炮兵团这一个由汽车牵引的炮团，装备苏式122毫米榴弹炮、76毫米加农炮和120毫米迫击炮，充当了军区的“火力拳头”。<br />
<br />
　　1956年康巴和昌都叛乱相继发生后，拉萨的反动上层同解放军驻军的关系随之紧张起来，藏军第２代本（编制相当小团）便长期占据乌瞰全城的制高点药王山并修筑工事。当时西藏军区大楼都在药王山上的步机枪火力控制下，干部们举起望远镜就能看到山头碉堡和备战的藏军，许多人抱怨“这简直天天在枪口下过日子”。虽然中央政府对达赖及噶厦政权采取了仁至义尽的争取，驻军坚持不打第一枪，叛乱分子却将忍让视为软弱可欺，在美、印反华势力支持下于1959年３月10日发动拉萨暴乱，公开宣布“起义”“驱汉”。<br />
<br />
　　３月１日拉萨暴乱发生时，7000余名康巴等地的叛匪涌进城内，上万喇嘛和市民被发放了枪支并做了战斗编组。藏军３个代本还逼近解放军各驻地修筑工事，甚至铁锹挖战壕扬起的土都甩到军区门岗内。此刻增援的第54军未到，解放军在拉萨城内只有第155团、第159团和军区警卫营的十三个步兵连、一个装甲车连和第308炮兵团三个营，兵力不足3000人，形势非常严峻。军区领导决定，一旦打响就要首先端掉药王山，才能转危为安。<br />
<br />
　　拉萨平叛打响前，第308炮兵团就对药王山上的各目标精确测定了射击诸元。３月17日达赖在藏军第１代本掩护下南逃，拉萨河边的解放军发现后，根据毛泽东５天前下达的“我军一概不要拦阻”的命令有意放行。３月20日10时，面对城内叛匪的多方进逼攻击，军区谭冠三政委下达了“打”的命令。一声令下后，第308炮兵团３发试射的炮弹就准确落在药王山顶的圆形碉堡上，瞬间把这个叛军指挥所炸飞。随后的一小时内，千余发炮弹呼啸着落到山上，所有碉堡战壕几乎都未及开火即被摧毁。11时后解放军以第159团一个连向药王山冲击一小时后未遇抵抗即登上山顶，发现藏军除被炸毙外已全部作乌兽散。<br />
<br />
　　解放军占领药王山后，为炮兵提供了俯视拉萨全城的观测所，兵力上以少击多就此有了准确的火力支援。20日下午，解放军向叛匪聚集的达赖夏宫罗布林卡发起攻击。炮兵根据中央关于不要损坏建筑的指示，用122毫米榴弹炮以徐进弹幕射击的方式轰击园内空地上的叛匪，以15米一个炸点逐次成一线推进。在猛烈火力打击下，上千叛匪窜出罗布林卡，炮弹却像长了眼睛一样追着落下，炸得其人仰马翻，拉萨城区的叛乱主力就此被粉碎。<br />
<br />
　　３月21日，解放军转入市内作战，大昭寺、小昭寺和布达拉宫等坚固建筑物成为叛匪顽抗的据点。为不损坏藏族传统建筑，炮兵只发射破坏力不大的120毫米迫击炮的杀伤弹，采取警示性射击以配合劝降，迫使大、小昭寺先后挂出白旗。22日晨解放军围攻布达拉宫，成为摧毁城内叛乱精神支柱的关键。这座雄伟建筑倚山而建，相对高度三百米，也是全城的一个制高点，盘踞其中的藏军第６代本一部武装喇嘛还向西藏军区院内开枪发炮。为了既不打坏建筑又要消灭顽抗之敌，第308炮兵团参谋长苗中琴集中两个步兵团的57毫米无坐力炮，在拉萨河北岸空地上瞄准布达拉宫向外开火的窗口实施精确射击。重庆兵工厂产的57毫米无坐力炮，是二次大战结束后仿美国制品试造成功的，于抗美援朝战争期间大量生产。50年代后期解放军全军换装苏式系列武器，内地部队淘汰了破甲威力不足的57毫米无坐力炮，西藏军区部队却继续保留。该炮包括三角架全重不过30千克，可两人操作，必要时可扛在肩上发射，射击精度较高，适合山地使用。对布达拉宫窗户射击的结果证明，不足３千克的弹头对建筑物破坏轻微，打入窗内一发却能全部击毙房内的几名叛乱分子。经过约一个小时人称“百发百中”的对窗射击后，布达拉宫内800余名叛乱分子惊恐万状，于22日９时挂出白旗，一个个走下山来投降。解放军进入布达拉宫搜索时，发现其内有各种枪械2500支，这个达赖居住的宗教圣殿实际上成了军械库。<br />
<br />
　　看到布达拉宫挂起白旗后，全城叛乱最后停止，许多被胁迫参叛者把白色哈达挂到户外，并将枪支扔到街上。解放军用卡车收缴枪支后对一般从叛者也不再追究。拉萨平叛结束后，叛匪分散到各地进行“游击战”，解放军重型火炮难以使用，平叛部队就主要携带57毫米无坐力炮、82毫米和60毫米迫击炮作为支援火力消灭叛匪火力点。美国飞机也向西藏空投了几批57毫米无坐力炮和60毫米迫击炮，却因叛匪缺乏复杂兵器的组织能力，作战时往往出现炮、弹脱节和步炮武器分离，结果未发挥多少作用，到头来都成为解放军的战利品。 <br />
<br />
　　杂乱武器中虽有精品，但标准化、制式化装备最终占优势 <br />
<br />
　　在藏区平叛作战中，解放军以航空兵、炮兵有力打击了大股叛乱武装，最终消灭分散“游击”的叛匪还要靠步兵武器。藏区参叛者人数和枪支数量都占优势，最终却不是解放军的对手，除了多数被裹胁者意志不强和战术水平低外，武器杂乱也是叛匪溃败的重要原因。<br />
<br />
　　从20世纪初至西藏解放前，藏区贵族主要向英印购买武器，同时也从青海、四川等地输入。从1949年至60年代，美国又秘密向叛乱分子供应装备，这使当地武器型号五花八门，步枪就有英、美、德、日和民国政府的多种产品，子弹口径各异。藏军购买量最多的步枪是英制李・恩菲尔德式，却有跨度近半世纪的十几种型号。参加过平叛的老前辈回忆，若组织火力双方对打时叛匪大多凌乱不堪，不过其枪法较准的冷枪袭击很具威胁性。康巴叛乱时解放军穿着新发的55式军装，军官戴大沿帽佩肩章，战士戴船形帽佩领章，干部往往很远就成了射击目标。<br />
<br />
　　1958年以后全军都改戴解放帽并全部佩领章，叛匪在远处才难以分辨官兵。<br />
<br />
　　1958年秋，窜入西藏的康巴叛匪恩珠仓・贡布扎西部从藏军仓库中得到20挺英国制MK１型“刘易斯”轻机枪（当时俗称“壮筒式机枪”），火力大为增强。该机枪由英国1915年开始制造，采用导气式原理从上部由弹鼓供弹。该枪因比较笨重（空枪重11.8千克），二次大战前即被英军淘汰，却仍有火力强、射程远的优点。此时因受西藏贵族女性诱惑叛逃的第155团炮兵主任、大尉姜华亭又投奔贡布扎西，为其充当参谋，这股匪部一时对解放军驻藏部队构成了较大威胁。<br />
<br />
　　同年12月18日和19日，贡布扎西、姜华亭指挥700余名叛匪伏击了赴山南的解放军第155团三个连的车队。叛匪居高临下以大批英制“刘易斯”机枪扫射，并使用了刚接收的美制M1步枪。在一面依山一面傍水的公路上，解放军车队难以展开火力，被动挨打，牺牲副团长、营长以下93人，负伤35人，成为藏区平叛作战中最严重的一次损失。<br />
<br />
　　从1958年到1961年，美国向叛匪空投了数千支步枪，主要是二次大战至朝鲜战争期间生产的“伽兰德”M1半自动步枪。该枪产量高达550万支，被称为20世纪军用步枪研制史上的一个里程碑，也是世界上大量装备部队的第一种半自动步枪。在美国“基尔营”受训的康巴叛匪试用了各种步兵武器后，声称最喜欢M1半自动步枪，其优点在于有效射程可达千米，因精度高可作为狙击枪，弹仓装弹８发且射速较快，在雪雨等恶劣条件下也能正常使用。该枪主要缺点是重达4.37千克，所用7.62×63毫米长形步枪弹也较重，体质差的人携带不太方便。平叛部队缴获该枪后许多指战员也很欣赏，却因太重和没有相配的子弹，只好弃之不用。<br />
<br />
　　美国向藏区空投和偷运的其它步兵武器，主要有加拿大制的英式“布伦”轻机枪、英国“斯登”式冲锋枪，这些二次大战时的著名武器却不大受叛匪喜欢。“布伦”机枪主要缺点是太笨重，子弹供应也困难。“斯登”冲锋枪有效射程不足百米，在植被很少视界开阔的高原上不能远距离开火。解放军对缴获的这类武器也不屑一顾，连民兵都不能装备而只当成废钢铁处理。<br />
<br />
　　参加西藏平叛作战的解放军部队，都按50年代全军统一的苏式标准配备步兵武器，包括仿“纳甘”步枪制成的53式步骑枪，仿“波波莎”41／43制成的50／54式冲锋枪，仿“德普”1927式制成的53式轻机枪，仿“郭留诺夫”重机枪制成的53式重机枪。除了冲锋枪与54式手枪通用51式手枪弹外，其它枪械统一使用53式枪弹。虽然上述武器都系苏联二战装备的仿制品，却形成了标准化、序列化，有利于组织火力和统一供应弹药。解放军同叛匪作战时除遇伏吃过一些亏，绝大多数情况下都能形成有效的远近程火力配系，并有充足的弹药保障，从而能确保在交战中压倒枪械杂乱、弹药不足且多不能通用的叛乱武装。<br />
<br />
　　苏联研制的轻武器有一个传统，便是结实耐用、价格低廉，虽不如美国和西欧枪械精良却威力不小，解放军在藏区平叛时使用的装备也表现出这一特点。当时步兵装备最多的53式步骑枪外观粗糙，远距离射击精度却很好，其全重3.92千克，弹匣容弹量５发，在高原使用故障少而很可靠，有较粗弹壳的“大屁股”弹杀伤力也不小。不过此枪靠手动枪机射速很慢，近战时往往来不及再装弹就同叛匪短兵相接，只好以刺刀搏斗。50式和54式冲锋枪分别重3.63千克和33千克，弹匣容量35发，近距离火力较猛且使用可靠，缺点是百米外精度很差，在视野开阔的高原上难以射击远距离目标。当时解放军步兵班都配有步骑枪和冲锋枪两种武器，才能在作战中取长补短。一些参战人员通过实战感到，应装备一种远程精度好又射速较高的自动枪，60年代初56式冲锋枪和半自动步枪的列装终于满足了这一要求。<br />
<br />
　　西藏平叛作战时解放军屡屡在公路上遇伏，没有装甲防护的汽车常常处于被动挨打的窘境，这也使国内军工部门开始研究如何让步兵乘装甲车作战的问题。1959年春。六一八厂和成都金属结构厂以改装方式紧急制造出国产第一种轮式装甲输送车，编号为533式，并于同年夏秋投入西藏的平叛和运输。该车利用苏联嗄斯－157汽车的底盘，再焊上６毫米钢板作为防护，并配备一挺12.7毫米机枪，可装载10人。该车型投入高原使用后虽能防范叛匪的枪弹，却暴露出动力不足、装甲薄且越野性能差的弱点，只能临时使用而未作为制式装备，不过它的研制还是开启了国内装甲输送车和步战车研制的先河。<br />
<br />
　　1956年至1961年的藏区平叛作战，是人民解放军在汉藏进步群众支持下同国际反华势力操纵下分裂势力的一场决战。其中在西藏地区从1958年至1961年进行的平叛作战，解放军消灭了2.3万名叛匪，招降了７万名受蒙蔽和胁迫参叛的群众。多年来西方媒体经常引用达赖散布的谣言，胡说什么当时“有120万藏人被杀”，若果真如此西藏早就没有人了！解放军在作战中也付出了1551名牺牲、1987人负伤的代价（不包括其它四省藏区平叛的伤亡）。当时战斗中的亡、伤比例远超过通常作战情况下的1∶3至1∶4，其主要原因一是遇伏时叛匪都要残杀伤员，二是高原自然条件恶劣造成救护难度大。<br />
<br />
　　西藏乃至整个藏区平叛的胜利，维护了祖国的统一并推动了西藏的社会进步。当年在分裂图谋失败后，藏区有叛乱分子及其家属数万人逃亡，达赖在印度建立了所谓“流亡政府”，康巴叛匪则在尼泊尔建立了骚扰内地的基地。经过几十年繁衍，国外流亡藏人至今已有十几万人，其中以达赖为首的一些人仍受国际反华势力豢养，继续进行分裂活动。事实证明，反对西藏分裂势力的斗争将是长期的任务，回顾历史上的平叛斗争至今仍有重要的教育意义。<br />
<br />
　　实战是检验武器效能的最重要标准。高原战场条件以及平叛一类的作战，都有其特殊要求，对于如何使用防暴型或震慑性、杀伤性武器也应进行有区别的分类研究。想有效达到既制止分裂又要争取各民族和谐的政治目标，在平叛、防暴一类军事斗争中应努力争取受蒙蔽群众和打击顽固分子，为此需要进行相应装备的研制和特定战法的研讨。<br />
<br />
　　［注］本文所提用的资料数据，均引自已经公开出版的书籍和回忆录，如2008年党史出版社出版的《西藏解放史》和国防大学出版的《中国国防导论》以及《西藏党史资料》等。<br />
