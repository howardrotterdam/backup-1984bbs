													<h2>鄢烈山天涯火贴：北京人为什么有那么多特权？</h2>
						
						
																											<div id="postmessage_292697" class="t_msgfont">.<br />
<br />
<br />
<br />
.<br />
<br />
<br />
<br />
<br />
.<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;之所以再次提出这个极其普通的常识性问题，是我的不公平感再一次被北京人就业、读书之外的特权深深地触动了. 我讲不清代表权、征税用税权和政务公开的大道理，也不拿美国首都华盛顿来比，更不想用“利益输送”、“构成既得利益集群”这样一些政治学术语来描述，我只想喊一嗓子“这不公平”！<br />
<br />
 　 1999年4月30日，我在北京的媒体发表过一篇《北京是谁的首都》；今天想在网上搜到电子文本，却发现网上广为传播的同名评论是“李曙明”2004年10月所写。我是有感于最高法院招人限于持北京户口者，李文是有感于2005年除了财政部外几乎所有国家部委在京的岗位都要求报考者具有“北京户口”。这个问题解决没有我不知道，也不想再说了，因为道理已讲尽，这不仅是就业歧视，而且是政治权利不平等；住处根本不是理由，外地报考者愿意录用后像“北漂”先赁居一段时间地下室又有何不可？<br />
<br />
　　 之所以再次提出这个极其普通的常识性问题，是我的不公平感再一次被北京人就业、读书之外的特权深深地触动了：<br />
<br />
　　 长假归来看报，10月12日《报刊文摘》头版头条转载的新华社记者专稿《国以民为本，民爱国若家》教我眼睛发亮。这标题把国与民、国与家的关系表述得多么透彻，“爱国主义”就这么简单，这么朴实！这道理这逻辑当然是对中华人民共和国全体公民适用的。可是，细读内容，你看――<br />
<br />
　　 “国庆前夕，北京地铁四号线开通了。轩敞的站台，高档的滚梯，还添置了触摸屏电脑供自助购票。可这种先进性让许多人不适应，刚把钱投进去，还没来得及选线路和终点，电脑就‘自说自话’地往外吐票、吐零钱了。尤其是习惯了按车程付款的外地客，往往吓一跳，直怨自己误操作。工作人员还得前来解释一番：在北京不管坐几条线，就是坐一天，也只收两块钱。<br />
<br />
　　 “在北京选乘公交，可真是便宜啊。可背后要是没有国力的发展，没有强大的公共财政支撑，哪能这么便宜呢？公交连着千家万户，公交的一大进步，只是国家改善公共服务进程中的微小一步。而公共服务的进步，正在形成国与家之间新的纽带。 <br />
<br />
　　 家是最小国，国是千万家；国以民为本，民爱国若家。……”<br />
<br />
　　 这真是很奇异的逻辑，由地铁等公交票价出乎外地游客意料的超级便宜，推论出适用于全体中国人的国与家关系论。难道1千余万北京人的感受就等于全中国13亿数千万人的感受？都门之外，如广州，地铁票和公交车票价比北京高数倍，13亿外地人又该如何认识国与民、国与家的关系呢？<br />
<br />
　　 的的确确，“背后要是没有国力的发展，没有强大的公共财政支撑，哪能这么便宜呢？”那么，北京公交的便宜靠的是北京市公共财政的支撑吗？既是，又肯定不是。<br />
<br />
　　 据广州媒体9月29日报道，中科院院士、建设部原副部长周干峙在会议上建议广州学习北京，在公共交通方面加大投入。周夸“北京的公交做到了多个世界第一……乘客只需要花2毛钱、4毛钱，像我这样头发白的根本不用掏钱”。对于这种“何不食肉糜”的建言，广州市长张广宁回应说“我也想（学北京），但是有难处啊。”他解释说，目前广州地区生产总值大概在8000亿元左右，相应的税收是2419亿元，这么多税收里有75%须向国家和省里上缴，广州只留了25%，加上国家和省里的返还大概到28%，“所以广州和一些直辖市没办法比……”这就是说，北京等直辖市的大量公共投入，其财力靠的主要税收留成政策的倾斜。这实际上是一种间接的转移支付。对西部等经济发展相对滞后地区搞直接和间接的转移支付是必要的，但对北京的这种转移支付却是以建设首都的名义进行的。<br />
<br />
　　 以前，人们(包括一些北京人士和在京媒体)，一是质疑中央财政向北京过度倾斜的必要性，政治性的首都不必什么都是大中心，如一些文体大工程不必集中建在缺水的北京，如北大清华占国家教育投入的比例不该空前地多；二是质疑固化户籍制度以保障北京户籍子女在上名牌大学、进国家机关工作等方面的特权。现在，我们从公交票价就可以感觉到北京人享受的经济特权，已太过分了。凭什么？广州市长不服，我作为广州市民不服，作为中华人民共和国公民和纳税人更不服！<br />
<br />
　　 我讲不清代表权、征税用税权和政务公开的大道理，也不拿美国首都华盛顿来比，更不想用“利益输送”、“构成既得利益集群”这样一些政治学术语来描述，我只想喊一嗓子“这不公平”！比毛泽东时代“三年困难时期”的首都特保（最基本）供应，还要不公平；比历朝历代对京畿地区的优待还过分。凭什么在广州上公共汽车就是2元，北京人才4毛钱？<br />
<br />
　　 “国”与“都”最古的词义就是城堡，城堡里面住着人数不多的“皇亲国戚”和侍从。21世纪的国与都绝不应将特权“复兴”到先秦之先。<br />
