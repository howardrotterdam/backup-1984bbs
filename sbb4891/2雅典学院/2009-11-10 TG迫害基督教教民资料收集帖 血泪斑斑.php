													<h2>天主教上海教区史话</h2>
						
						
																											<div id="postmessage_321874" class="t_msgfont">1608年（明万历三十六年），在京任职的天主教徒徐光启（1562-1633），回乡为父守孝，途经南京，邀请意大利耶稣会士郭居静（Lazzaro Cattaneo）神父去上海传教，徐氏家属亲友二百余人受洗入教。1640年，意大利传教士潘国光建立了上海第一座公用天主教堂敬一堂。到18世纪，上海一带已有教友达万余，构成南京教区的主要部分。禁教期间，敬一堂被改作他用，但天主教信仰已经在附近农村地区扎根，许多人在地下状态中坚持了信仰。<br />
<br />
南京教区的最后一位主教是意大利传教士罗伯济。1856年，南京教区改为江南代牧区，交给1842年已经来此、1847年在徐家汇建立总部的的耶稣会巴黎省会的法国神甫管理，管辖范围包括江苏、安徽2省。<br />
<br />
1921年，分为南京代牧区（主教座堂在上海）和安徽代牧区（主教座堂在芜湖）。1926年和1931年，再度分出海门教区和徐州教区。1936年，江苏省剩下的部分分别设立上海代牧区和南京代牧区，上海代牧区管辖长江以南苏州以东的地区，和长江以北除徐州和南通附近以外的地区（大半个苏北，但信徒较少）。<br />
<br />
1946年，罗马教廷宣布成立中国圣统制，上海宗座代牧区改设正式的上海教区。1949年，从上海教区又分出苏州教区和扬州、海州两个监牧区，上海教区和苏州教区均由中国籍主教管理。1950年，苏州主教龚品梅兼任上海主教。<br />
<br />
上海解放不久后，中共当局即逐渐升高对宗教的敌视，没收了教会的学校，慈善机构。封闭修道院及神学院，龚品梅所领导的上海天主教却欣欣向荣，并没有在高压政策下畏缩，到了一九五三年以后，中共成立了“三自爱国教会”强迫所有的天主教徒加入，龚品梅及上海的信徒不为所动。1955年9月8日深夜，中共出动了大批军警，逮捕了龚品梅主教和上海徐家汇大小修院内的神父和修女，和上海市的神父修女，从而关闭了上海天主教所有教堂和修会全院，中共上海市当局虽然在监狱，施尽各种手段，迫使龚品梅主教屈服就范，但都以失败告终。他们最后就运用群众批判斗争的运动来威胁龚品梅主教。中共当局在上海市内的一个大体育场内，特地召开了一次对龚品梅主教举行群众批判斗争大会，中共召集了数万上海市民来听龚品梅主教公开“认罪”。他的双手反绑，穿了一套宽松衣裤。这位身高162公分的主教被推到麦克风前认罪。然而从龚品梅主教口中发出的，是正气凛然的呼声：“基督君王万岁！教宗万岁！”让在场的秘密警察大为震惊。群众也立刻回应：“基督君王万岁！龚主教万岁！”龚品梅主教立刻被拖上警车，直到一九六○年他被送到法庭审判之前，再也没有露面。那一年，龚品梅主教被判无期徒刑。<br />
<br />
1966年文革开始，所有圣堂全部关闭，挪作他用。<br />
<br />
1985年，龚品梅主教从狱中获释之前不久，菲律宾马尼拉的辛枢机到上海做友好访问，龚品梅主教获准参加由上海当局为欢迎辛枢机而主办的晚宴。这是龚品梅主教自入狱以来，第一次见到普世教会来访问的主教。辛枢机和龚品梅主教坐在桌子的两头，中间隔了二十多名共产党员，两人没有机会私下交谈。在晚宴中，辛枢机建议每个人唱一首歌来表示庆祝。轮到龚品梅主教唱时，他当着政府官员和爱国会主教的面，双眼注视着辛枢机，然后唱“你是伯多禄，在这磐石上我要建立我的教会”(Tu es Petrus et super hanc Petram aedificabo Ecclesiam meam)。这首圣歌，是表明承认教宗的至上权威。龚品梅主教向辛枢机传达了一个讯息：在他被监禁的这些年里，他始终忠于天主、忠于教会、忠于教宗。<br />
<br />
晚宴结束后，中国天主教爱国会的、自选自圣的上海主教金鲁贤斥责龚品梅主教：“你想干什么？表示你的立场吗？”龚品梅主教平静地回答：“我不需要表示我的立场。我的立场从没有改变过。”<br />
<br />
辛枢机立刻把龚品梅主教的口信转达给教宗，并向世人宣布：“这位天主的人，尽管遭受了难以想像的苦难与隔离，但他对教会或对他子民的爱，却从未动摇过。”<br />
<br />
一九九一年六月二十八日，教宗若望保禄二世在梵蒂冈的枢机主教全体会议上，授给龚品梅主教枢机头衔，这位已高龄九十的主教，从轮椅上站起来，把手杖放到一旁，走上台阶，跪在教宗脚前。教宗显然深受感动，把他扶了起来，授给他枢机的红帽，然后耐心地站在一旁，看着龚品梅枢机在九千名来宾的起立欢呼声中走回轮椅，欢呼声足足长达七分钟，是前所未见的热烈。<br />
