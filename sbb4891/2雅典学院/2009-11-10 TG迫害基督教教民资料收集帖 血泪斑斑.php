大事记：<br />
<br />
九八教难历史档案<br />
<br />
一九五五年九月八日深夜，中共当局出动警察军队，逮捕了上海教区首牧龚品梅主教和三十多位神父及三百多位平信徒，制造了所谓“龚品梅反革命集团”的冤狱案，神职人员及教友分别判了十年到终身劳改的徒刑，龚品梅主教本人则判终身监禁，关在上海提篮桥监狱，全国各地的教会人士，也在九月同时受到逮捕。<br />
<br />
龚天爵（品梅）主教，饮了三十三年“苦”“爵”的主教，今天获得了天上的“天爵”。在戴上红帽，当上主教，以“生死不离教宗，不离基督教会”，牧养好上海、苏州、南京三地教友。教宗赞扬枢机是“中国教会高贵之子”。 <br />
沈士贤神父，海外留学归来在上海岳阳路天主教教务协进会（Catholic Central Bureau简称C.C.B.）工作，在震旦女子文理学院任教，博学多才，深得青年人的心。上海解放前夕（1949.4），文理学院能有十位青年领洗进教，了不起。领洗前的考题：①教难将来到，为什么你这时候要进教，不是讨苦吃吗？②要领洗，把你的血带来！谢宗瑶、胡美珍、胡美玉就是其中三位，而神父自己实践了为教会献出自己的鲜血，到1953年还没判，却死在上海市监狱。 <br />
陈哲敏神父，罗马归来同沈士贤神父、候之正神父一起在C.C.B.工作，并任震旦大学伦理学和哲学教授，教廷公使黎培理主教的秘书。著有“圣而公教会”一书批50年代的“三自”，51年在C.C.B.被捕，60年同主教等一起判刑，判20年，62年死在安徽白湖农场，临死前同傅鹤洲神父的一段谈话今天存在“殉道史资料室天主忠仆一书”中。神父的：“爱国运动”要与“宗教内部问题”分清，如要有所表示，不能混在一起。 <br />
候之正神父，四川人，和陈哲敏、沈士贤同在C.C.B.工作，判决书中有其名，但注明已在监房内病逝（新华社1960. 3.17报导）。 <br />
张希斌神父，上海教区咨议员，主教的帮手，在上海教区教难中神父的言论起了大作用，并引导许多知识份子认识真理。在60年龚品梅一案中，三位神父（张希斌、陈哲敏、朱树德）“没有悔罪表现”，用“罪行重大”。判刑20年。 <br />
傅鹤洲神父，上海教区咨议员，主教的好帮手，得力助手。神父有经济头脑，善理财，负责全教区的房地产和教区财务。判15年送安徽白湖，刑满后1981年11月19日再被捕。五十年代主教要调傅神父来洋泾浜主教府，神父已早说了这是吃“官司”的位子，我绝对服从主教，我去了。神父头脑清，有聪敏之恩，在教难初期有功于上海教区，1960年检察院说；傅鹤洲一贯替龚品梅出“坏点子”。 <br />
张伯达神父，上海教区第一位捍卫“教会圣统”殉道的耶稣会士，给中国的神父们开了一扇殉道之门，教区的胜利。1951年伯达神父在会上提出让中国天主教主教团拟出一份方案供双方研究，说得合情合理，8月9日的逮捕下狱显得不合情理，11月11日惨死提篮桥监狱是个“不明”“不白”，但有一天会彰显天主的公义。 <br />
王仁生神父，同张伯达神父在华东私校教育会议上捍卫教会圣统，明确表态的两位耶稣会士，1953年7月7日夜同陈天祥神父，蔡石方神父，傅鹤洲神父一起被捕。仁生神父1960年3月7日公审判决后送安徽长江边荻港，11月腿肿进病房，12月20日病重行傅油礼，22日半夜去世。” <br />
朱树德神父，1949年10月19日树德神父奉耶稣会总会长之命“去中国”，从巴黎飞到香港，“…出大陆进香港的人，不计其数，但出香港进大陆的，恐怕只我一人，回上海干什么？我确是天字第一号大傻瓜！不，我是神父，我是要回去，我代表耶稣基督的教会，我在那里，教会便在那里。我留在上海，叫共产党知道天主教还存在着。”（家信）到上海树德神父当了震旦大学公教学生的神师，他在震旦放了第一把看不见的火，“教理之火”烧了起来蔓延到整个上海的院校，全教区从小朋友到大学生都活跃起来，灵魂活跃起来，信德活跃起来，教区活跃起来，作了全国教区的榜样。 <br />
陈天祥神父，咨议员、耶稣会长秘书。判决后送往江西马档，有朱雪帆副主教、朱洪声、陈天祥、陈云棠、蔡石方等神父。天祥神父在江西水牢中著有“狱中之歌”歌词：主要我生，谁能死我？主要我归，谁能留我？在1951年11月13日龚主教与耶稣会格会长带领全体神父、修士、教友替张伯达神父在君王堂举行追思弥撒，伯达神父是殉道死的，主礼神父穿的是红色祭披，由陈天祥神父证道，证道最后一句是这样的：“我们应该走的道路，敬爱的伯达神父已走给我们看了，我们再没有迟疑的可能，生路只有一条，就是跟伯达神父走上苦难的加尔瓦略山。”50年代已有好几位神父、教友像伯达神父一样死在监狱中。 <br />
吴应枫神父，1953年的圣诞夜，三自革新分子混在教友中故意干扰子时弥撒要领圣体，在虹口圣心堂，领圣体前吴应枫神父果断向全堂教友宣布：我现在向在场的三自革新分子讲清楚请你们勿自动上前领圣体，否则给我拖出去。神父的威严正气使全堂教友热心望完这台子夜弥撒。又给我们上了一课，革新的教友为什么不能领圣体？──神父圣名若瑟，坚振圣名若望，“九八”夜被捕，判15年，送安徽白茅岭，因体力不支，死于1977年8月，是年78岁。 <br />
蔡石方神父，上海教友很多喜爱听神父的道理，在那年代上海教区轮值堂口每主日下午圣体降福前的教理讲座，听道理的教友正是人山人海来形容，教区有好牧人领导，教区有博学多才又有圣德的神父作灵修指导，上海的教友是真热心，能经历50多年不变保持到今天，尽管神父们一一走了。 <br />
朱洪声神父，朱洪声神父14岁随叔父朱开敏主教（第一批六位中国主教之一）前去比利时，法国，爱尔兰，美国，1947年回上海任徐汇中学教务主任，1951年教会学校没收调上海君王堂，受青年爱戴，“九八”被捕，1960年判15年，1981年11月19日再捕再判15年，1993年2月上海中级人民法院宣布撤销原判，恢复公民权，7月6日魂归天国。 <br />
陈云棠神父，逮捕前为徐汇小修院院长，后任圣伯多禄堂院长，为堂口教友每月领月省，组织领导老年教友的教理班，堂口生气勃勃。判12年，同陈天祥、蔡石方、朱洪声、朱副主教同在江西马档劳改。 <br />
沈百顺神父，耶稣会士，热心孝爱圣母的神父，为忠于教宗、忠于教会，他比他人走得不同──逮捕－释放－再逮捕－再释放－又逮捕－不再释放死在上海监狱。 <br />
严蕴梁神父，“圣母神父”，上海教友这样称呼特别热心孝爱圣母的严蕴梁神父，内修深蕴，圣德卓著，博学多才，五十年代任徐汇修院神师，主编神修文库，翻译圣经（上海教友用的新经.上.四福音），给中国教会带来丰富的灵修食粮。“九八”同主教一起被捕，三十年监狱生活，把基督的爱带给了周围的人。著有“与造物八日游”“福音中的圣母像”“玫瑰集”“苦路集”“方尖碑”和自传“游子吟”…等。 <br />
陆达源神父　1950年由龚主教祝圣为神父，后在修院教文学及翻译，1955年被捕，一年后获释，不久再入狱长达二十一年，1990年移民加拿大，1991年罹患血癌去世。 <br />
李季才神父　“九八”入狱，判五年，留场劳动改造过了五个五年。释放后回离佘山不远的九里亭家，时神父已患有肝癌，邻近乡村教友十分爱戴两位神父，哥哥李仲安神父，妹妹是仁爱会修女。 <br />
朱者赤神父，逮捕后不久死在上海监狱。 <br />
朱雪帆副主教，圣名西尔物斯德肋，1902年生于上海，1955年12月被捕，1960年3月17日被判十年，剥夺政治权利四年。后解往江西马档，期满送上饶，由于年老体弱，生病在床无人照顾，病死劳改营。 <br />
王方、章献猷、陈西、马凤祥、张维翰、郭学敬、秦士元、沈乐天…神父、朱菊初修士、俞祥琨修士、沈多才先生、张登义先生、饶克安先生、金铎先生、姚天声、叶兆纶（十四岁少年）、史献芝修女、周淑英修女、沈梨霞、张玉琴、朱雅宝、张茵秋…<br />
