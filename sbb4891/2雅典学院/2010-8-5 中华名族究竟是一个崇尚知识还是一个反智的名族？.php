																											<div id="postmessage_771806" class="t_msgfont">“皇权独大”，崖山之后大概如此。汉唐宋不能这么说。科举制在异族统治时期不仅失去了它作为选官制度的意义，也使得真正的士大夫阶层就此消亡。钱穆《中国历代政治得失》里“制度与法术”一节是这么说的：<br />
&nbsp; &nbsp;&nbsp; &nbsp;我们讲政治制度，有一些确实是制度，有一些则只能叫做事件或法术。制度指政治而言，法术只是些事情或手段；不好说是政治。大抵制度是出之于公的，在公的用心下形成的一些度量分寸是制度。而法术则出之于私，因此没有一定恰好的界限。所谓方法与权术，二者之间，当然又不能仔细分。而且一个制度之成立，也当然有许多复杂关系，总不免夹带有当时一些私意的。要说建立一制度，而绝对地大公无私，不仅古代历史未之有，就是将来的历史，要说一个国家建立某项制度，而绝无人事关系，绝无私心夹杂，恐怕这希望也还远。不过公私之间该有分量的轻重。现在再说中国历代政治制度究竟是出于公的多呢？还是出于私的多？究竟法术的意义重呢？还是制度的意义重？论汉代，西汉可说是制度，东汉则多半出于光武的私心。论唐代，确实可说在建立制度，而宋代则有许多只算是一种法术。明代，有许多只能说它是一些事，不能说它是一些制。尤其是清代，可说全没有制度。它所有的制度，都是根据着明代，而在明代的制度里，再加上他们许多的私心。这种私心，可说是一种“部族政权”的私心。一切有满洲部族的私心处罚，所以全只有法术，更不见制度。</div>

							
							
							
							
