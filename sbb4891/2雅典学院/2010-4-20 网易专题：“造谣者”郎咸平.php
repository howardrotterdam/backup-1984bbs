 <br />
 <br />
 <br />
<br />
 <br />
转基因：广西转基因玉米是美国的基因武器 <br />
 <br />
纪录片《孟山都公司眼中的世界》宣传画。这部新纪录片对美国孟山都公司的名誉发起了另一轮剧烈冲击。美国孟山都公司是全球最大的化工公司之一，也是全球90%转基因作物的种子技术的提供者。这部名为“孟山都公司眼中的世界”的法国纪录片由独立导演Marie-Monique Robin执导，影片为观众展现出一家长期危害环境、制造卫生健康丑闻的公司的可怕嘴脸。<br />
 <br />
<br />
 <br />
<br />
 <br />
&nbsp;&nbsp;<br />
郎氏言论：论文称孟山都“迪卡”系列玉米对危害人体 <br />
 <br />
广西新闻网2009年11月19日曾做过《广西在校大学男生性健康，过半抽检男生精液不合格》的报道。与此同时，同样还有一则官方报道：广西已经和美国的孟山都公司从2001年至今在广西推广了上千万亩“迪卡”系列转基因玉米。郎咸平称，玉米作为广西水稻后第二大主食的农作物，至今还是广西5000 万人中1000万人不可缺少的主要口粮。玉米在美国只是作为工业原料来种植，广西广大农民在不知情的情况下种了1026万亩。 <br />
 <br />
他还说，三位“非常出色”的法国科学家还做了各种精湛的估计和分析，揭露了孟山都这两个种子的真实影响。他们这篇论文发表在2009年12月一期《生物科学国际期刊》上（网址），结论是，三种孟山都公司的转基因玉米能让老鼠的肝脏、肾脏和其它器官受损。&nbsp;&nbsp;<br />
 <br />
事实：论文研究对象非孟山都的“迪卡”系列 <br />
 <br />
广西广泛种植的“迪卡”系列玉米从来就是杂交玉米，而非转基因玉米。到现在没有任何人有证据证明“迪卡”是转基因玉米。 <br />
 <br />
而首先这篇论文研究的孟山都转基因玉米品种分别是NK 603, MON 810, MON 863，根本不是郎咸平说的“孟山都这两个种子”（指用来培育迪卡007的PA212和PA31）。其次，这三个法国人根本没有做过任何试验，他们在论文中说得清清楚楚，采用的是孟山都提交给欧洲政府公开的实验数据。也就是说，这个被郎咸平称赞为“非常仔细的实验，都是孟山都自己做的。第三，这三个法国人是用孟山都的实验数据，用一套没有获得公认的统计方法重新进行分析，才有了所谓“惊人发现”。对这篇论文的结果，法国生物技术高级咨询委员会并不认可，认为这篇论文没有提供任何可接受的科学证据（文档）。郎咸平提到的其他几个实验，也都存在类似的问题，不被认可。&nbsp;&nbsp;<br />
