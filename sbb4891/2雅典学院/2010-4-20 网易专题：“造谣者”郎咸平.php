																											<div id="postmessage_572223" class="t_msgfont">铁矿石谈判：华尔街操控国际铁矿石价 <br />
 <br />
据郎咸平描述，油价跟波罗的海指数就是华尔街决定的。因为你要正本清源，要釜底抽薪，如果你真想得到便宜的铁矿石的话，你就必须拜托高盛把油价和波罗的海指数拉低。这就是游戏规则。 <br />
<br />
 <br />
 <br />
中国对铁矿石消费需求极大，但国内的铁矿石品位太低，而且根本无法满足需求。 <br />
<br />
 <br />
&nbsp;&nbsp;<br />
郎氏言论：美国通过石油和波罗的海指数控制铁矿石价格 <br />
 <br />
郎咸平称：通常从经济学角度分析，商品的价格是由供求关系决定的，但铁矿石不是。只要做过铁矿石生意的人都知道，铁矿石或者叫铁矿砂多得跟海边的沙子一样。所以说他的价格不是供求决定的，而是由华尔街决定的。&nbsp;&nbsp;<br />
 <br />
2009年2月11日，波罗的海指数一举突破2000点，达到2055点。在全球经济一片萧条的景象中，这一强势翻转立即被找到了答案，那就是运往中国的铁矿石。对于铁矿石与其他能源矿产之间的彼此关联越来越高，而铁矿石需要油轮跨大洲运输，也与石油的价格密切相关。 <br />
 <br />
原因是铁矿石价格与波罗的海指数和原油价格紧密相关，而这两项的价格是由华尔街决定的。如果你想得到便宜的铁矿石，那就必须找高盛，拜托它们把波罗的海指数和原油价格拉低，这就是游戏规则。铁矿石价格决定于石油价格和波罗的海指数，而油价和波罗的海指数的背后是美国政府。&nbsp;&nbsp;<br />
 <br />
事实：波罗的海指数是反映运费价格的指数 <br />
 <br />
波罗的海干货指数2008年12月5日的历史低点为663点，而2009年2月11日反弹到2055点，上涨三倍，似乎看上去是有人在全球经济一片萧条中操纵波罗的海干货指数。实际上，国际航运价格一直波动巨大。该指数在2008年12月5日的半年前，达到了历史最高点11612点。半年之内下跌到了历史最高点的5%，再反弹到历史最高点的15%。目前依然在2900点附近徘徊。 <br />
 <br />
BDI波罗的海指数是由几条主要航线的即期运费（Spot Rate）加权计算而成，反映的是传统的干散货船航线的运价，因此，运费价格的高低影响到指数的涨跌。所以，假设现在的原物料需求增加，表示各国对货轮运输的需求也跟着提高，在此情况下，运费的涨幅如果可以大过油价成本的涨幅，当然其获利就可维持。在这种前提下，BDI波罗的海指数会上涨，运输股的股价也会上涨；假设现在原物料需求依旧增加，但货轮供给增加而打坏运费市场，此时BDI波罗的海指数会下跌，当然运输股的股价也会跟着下跌。 <br />
造谣鉴定：倒果为因 <br />
 <br />
是铁矿石等大宗商品的贸易额不断上涨决定了航运价格的变动，而非航运价格决定铁矿石价格。而华尔街控制国际航运价格更是无稽之谈。郎咸平根本没解释华尔街是如何控制航运价格的。统计学上最基础的原则之一是，相关性不等于因果性。比如，今年20岁的小明，过去10年里，每年的年龄增加1岁，大概是10%到5%之间，同期，中国GDP每年上涨5-10%之间。难道小明的年龄增长就决定了中国GDP的增长？ <br />
常识错误：铁矿石并非多得像沙子 <br />
 <br />
郎咸平说，铁矿石也称铁矿砂。其实，只有巴西产的“粉矿”为铁矿砂，其他国家的铁矿石都是真正的石头。而如果铁矿石多的就像沙子一样，根本无法解释为什么中国就不能自己生产足够的铁矿石？中国的铁矿石消费极大，但国内的铁矿石品位太低，而且根本无法满足需求。 <br />
