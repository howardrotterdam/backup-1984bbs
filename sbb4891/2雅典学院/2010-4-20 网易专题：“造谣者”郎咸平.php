2009年6月众议院通过法案，授权美国政府对于出口到美国的产品征收碳关税，每吨碳征收10～70美元。举个例子，生产一吨水泥就会产生一吨二氧化碳，一吨二氧化碳就要缴纳10～70美元的关税。想想看，有多贵？倒时候每个中国人呼吸产生的二氧化碳也将上税。 <br />
 <br />
事实：只针对拒不履行减排义务的国家 <br />
 <br />
所谓6月26日众议院通过的法案，是《美国清洁能源与安全法案》，被认为是美国终于承担起世界最大碳排放国家的责任的法案，得到国际社会的一致称赞。美国不仅承诺了单边减排，而且它提出了比奥巴马总统当初倡议的更为激进的二氧化碳减排目标：在2005年排放量的基础上，到2020年减排20%（奥巴马的提议是14%），到2050年减排83%。法案一旦生效，美国85%的行业和领域将会被涵盖。&nbsp;&nbsp;<br />
 <br />
整个法案95%以上的内容是在约束美国企业，约束美国经济。美国将为此付出每年数百亿美元的代价。而其中仅有一条谈到征收碳关税：授权美国政府今后对因拒绝减排而获得竞争优势的国家的出口产品征收“碳关税”。而这个碳关税，是经过核查的超额减排的碳关税。比如生产水泥必然会产生二氧化碳，但如果使用非清洁能源产生的超额二氧化碳，则需要征收关税。 <br />
 <br />
更进一步的是《美国清洁能源与安全法案》仅仅是通过的众议院，到现在都还没通过参议院。也就是说根本还不是美国法律。 <br />
造谣鉴定：隐藏事实、偷换概念 <br />
 <br />
美国清洁能源与安全法案作为奥巴马政府一改布什政府的孤立主义情绪，积极参与国际减排合作。在美国受到共和党的抵制，目前依然无法通过参议院的认可。而法案主要内容是规制美国企业和美国经济的碳排放。对于未来可能的”碳关税“，对于全球的低碳化进程，也是有好处的。 <br />
 <br />
另外，郎咸平将对使用非清洁能源而产生的超额二氧化碳征税偷换概念为对所有二氧化碳征税，将一项还在制定中的法案等同于已经推出的法案；同时更进一步扩展为因为中国人呼吸要产生二氧化碳，因此说未来中国要向美国缴纳呼吸税，这明显是偷换概念。 <br />
<br />
