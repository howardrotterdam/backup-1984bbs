郎氏言论：曾任多所美国名校教授 <br />
 <br />
在众多网站中对郎咸平的经历是这么介绍的：美国宾西法尼亚大学沃顿商学院博士；现任香港中文大学讲座教授；曾任沃顿商学院，密西根州立大学，俄亥俄州立大学，纽约大学和芝加哥大学教授。其在多个场合自称曾任沃顿商学院、密西根州立大学、俄亥俄州立大学、纽约大学、芝加哥大学教授。 <br />
 <br />
事实：只是美国助教，从未担任过“转正的教师” <br />
 <br />
郎咸平正式拥有的正教授头衔是香港中文大学教授和长江商学院教授，其它的所谓教授都不是正教授，郎咸平在美国基本上是没有Tenure（Tenure指转正的教师）的讲师和副教授。在美国， Tenure，也就是转正之后的大学教师分为三级：Assistant Professor（助理教授）： 相当于中国的讲师；Associate Professor（副教授）：相当于中国的副教授；Professor （教授）：相当于中国的正教授。 <br />
 <br />
事实上，在郎咸平现在工作的香港中文大学网站上，对其工作经历的介绍，就较为真实:“郎教授在先后于八五及八六年取得宾夕法尼亚大学文科硕士和哲学博士学位。毕业后，郎教授获原校聘任为财务学讲师，八七年转任密执安州立大学助理教授，八八年出任俄亥俄州立大学访问助理教授，八九年起任纽约大学斯特恩商学院助理教授，四年后晋升为副教授。”――其在美国他没有得到过任何正教授的职称。 <br />
