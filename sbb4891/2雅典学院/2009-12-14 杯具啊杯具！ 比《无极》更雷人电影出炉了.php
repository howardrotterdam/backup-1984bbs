																											<div id="postmessage_373198" class="t_msgfont">1、首先请注意――未成年人及卫道士请自觉忽视脏话，这并不会影响你的阅读。<br />
<br />
　　2、请所有看到我这篇文字的朋友帮忙转帖，分享，我希望更多的朋友看到它，因为这篇文字不是出自一个枪手，它的背后也没有任何商业炒作的目的――这完全是一个普通观众，普通公民，普通百姓对某些电影的正是感受和愤怒！我只是一只小蚂蚁，我见不到那些位高权重的人，我也见不到那些大明星！我更没有任何机会在媒体上奔走呼号！我只能在论坛上，BBS上发表自己的观点，发泄自己的愤怒！希望所有和我一样的普通水民，和我一样的弱势群体能够听到我的声音！<br />
　　<br />
　　3、以下涉及到的几部电影――《麦田》《刺陵》《风云》，本人绝不做任何正推荐，强烈建议大家连DVD都不要买，看盗版都是浪费时间！如果非要花钱看个景活着看个人，建议大家做好被羞辱的心理准备，再抱着绝对批判性的心情。至少这样你的失望会少一些！<br />
　　<br />
　　我是一个普通的观众，我叫小九。我刚从中关村的美嘉电影城出来，一脑门官司钻进星巴克，为的就是赶快找个坐下的地方，一吐我心中的恶气！作为一个良好的市民，我奉公守法了这么多年，忍气吞声了这么多年，埋头苦干了这么多年，今天终于忍无可忍的爆发――只因为我看了今年最让我恶心的一部烂片――《刺陵》！<br />
　　<br />
　　好吧，我承认，我是先看了《风云2》，又赶场看的《刺陵》，本来就有些怨气，所以聚在一起爆发，也不能说全是《刺陵》的关系。我翘了班，打车赶到电影院，兴冲冲地买票，急不可待地冲进电影厅，怀着无比兴奋和幸福的心情期待着《风云2》和《刺陵》带给我期待已久的激情，结果――被《风云2》泼了一桶冷水，又被《刺陵》泼了一整个浴缸的粪水，我实在不能控制自己的心情，当我起身时，我不断地对自己说――“冷静！千万要冷静！你身上就带了几百块钱，如果你把手上的可乐丢到屏幕上，你赔不起！”我不断这样暗示着自己走出来，终于没有做出损害公共财物，破坏公共秩序的行为。<br />
　　<br />
　　本以为，《麦田》那样史诗级巨二无比的电影，一年能出一部已经不错了，可是真没想到，巨二年年有，今年特别多！《追影》二也罢了，但它是吴镇宇首执导筒，一个演员出身的导演，很诚心诚意地去搞笑，就算故事讲得差点我们也能接受，只希望他今后干回老本行，认认真真演戏，老老实实当影帝，也就是了。<br />
　　<br />
　　《麦田》二也罢了，虽然何平从《天地英雄》开始就在毁我，虽然大导演真正做到了数年磨一“二”，继续认真地强奸着艺术，鄙视着观众的智商，但是至少何平找到了海拉尔，拍出来我认为今年风景最美的地方，当风景来看，也还能忍受。<br />
　　<br />
　　《风云2》至少也还二的真诚！前者彭氏兄弟已经在首映时放出话来――这是一部漫画式的电影，尊重马老原著精神，视觉特效是主打；后者兄弟两个人也确实说到做到，虽然整个片子似乎没有一个识字的编剧，至少画面和特效确实做得足够漂亮！我还没等到《三枪》上映，如果没有张艺谋，我想这部片子的视觉效果应该是今年华语片中最出色的。而且城城和伊面两个不服老的帅哥也确实养眼，整个戏虽然看得无聊，至少不会瞌睡。<br />
　　<br />
　　可是《刺陵》呢！可是《刺陵》呢！！<br />
　　<br />
　　请允许我问候投资人和制片人全家，顺便问候朱延平本人！<br />
　　<br />
　　虽然我身边的几个小姑娘是冲着周杰伦去的，每次周杰伦打斗时她们都会说：“好帅好帅！”可是我确确实实是冲着朱延平的名字去的。以前他拍过什么我不知道，只是去年看他的《大灌篮》，结结实实地喜欢了一把。我以为他是个好的娱乐片导演，他能把故事讲得至少清楚明白，而且还很搞笑。所以这次――我也是怀着同样的期待，等了足足两个月！我想：宁浩可以拍完《疯狂的石头》后还能拍出《疯狂的赛车》，你朱延平总还至少不会差太多吧！可是你这次为什么偏偏要挑战你不擅长的寻宝题材？这也就罢了，如果你诚心诚意去尝试，哪怕是部失败的作品观众也可以原谅你，可是你却在侮辱我们这些老实巴交诚心诚意期待你能出好作品的人！你这算什么？？<br />
　　<br />
　　从一开场，便是露骨到极点的嵌入广告！几个大特写和故意恶心观众的logo下来，我们记得了百度，起点中文网，诺基亚新款手机，可是情节呢？情节呢？一个被催稿的美女畅销书作家，一个在沙漠中耍酷的寻宝者？这两个人是咋回事？随着情节展开，俩人终于有了交集，原来美女作家和寻宝者小时候认识，寻宝者又貌似是替美女爸爸工作的？而且貌似是比较正义的工作？狗血的剧情一个个展开，每一个我都早已在其他电影里看了无数遍呀无数遍！<br />
　　<br />
　　狗血1――美女爸爸得到一张地图，并因此而死。<br />
　　狗血2――美女和爸爸从小父女关系不好，爸爸死了美女才感到后悔。<br />
　　狗血3――男主和女主从小就认识，俩人都对对方有点意思，可是又打死不承认，一个劲地玩暧昧。<br />
　　<br />
　　狗血4――硬加进去原本负责插科打诨角色的两个跟班，却从头到尾没共享一次搞笑。<br />
　　狗血5――好好的黄一飞在里面和一个打铁的光知道露脸，对剧情却没有任何共享，纯属多余，纯属在浪费他们！<br />
　　狗血6――莫名奇妙出来杀了女主爸爸的木乃伊，跟男主打了一次就跑了，后来再也没有出现，他是出来打酱油的么？<br />
　　<br />
　　狗血7――生猛的沙尘暴军团，莫名其妙的浪人部落，这些换成其他的马贼什么的又有什么不同？完全换汤不换药的无创意噱头。<br />
　　狗血8――朱导开头设置的包袱在最后揭开，简直弱智得让人觉得可笑都笑不出来！那些一眼就能猜到的狗血剧情，太让人内伤了！<br />
　　狗血9――曾志伟还是在里面穿线，陈道明还是一如既往地装酷，可是俩人一个演得是贪财的人，却完全没有贪财的专业精神，辛辛苦苦找到金砖后最后竟然都主动扔了，摆明告诉大家我是听从导演安排必须不顾人情逻辑扔下金砖的。而陈道明，从头至尾负责演绎一个受过创伤的忧郁症患者――你丫不就是靠吃同伴尸体才活着走出大漠了么？至于最后还回来殉情，弄得天愁地惨么？你的同伴又不是你杀的，再说你跟他们感情好我们完全看不出来，不就是几十年前一起合了一张影么？<br />
　　<br />
　　狗血10――杀死女主爸爸的人似乎就是那个装木乃伊的帅哥，打了酱油后就回家坐俯卧撑去了，好像女主爸爸死得其所的样子，最后导演也没有找个把人给他报仇。<br />
　　狗血11――整个故事到底你想说什么？是英雄美人沙漠寻宝么？可是那些故意牵扯出来的纠葛，什么沙漠飞鹰，什么戴着个假面具的部落女首领，什么沙尘暴军团，什么最后出现的四个老不死鬼魂，都是干嘛的？看电影时，我的脑海中不断闪现着《国家宝藏》和《神话》，我简直想高声大叫：“这丫就是画虎不成反类癞皮狗的典型四不像呀！！”<br />
　　<br />
　　狗血12――整个电影，关键的几处道具――宝藏地图，骷髅地形的古城，莫名其妙出镜率极高的龙卷风，明显看过《鬼吹灯》才编出来的尸花，最后终于发现却莫名其妙化为灰烬的夜明珠，当然还有一点理由没有，就是那么倔脾气非要最后不能免俗塌掉的地洞，非要在主角们慢悠悠走出门后关上的石门，非要在陈道明身边落下却偏偏不愿意砸中他脑壳的大石……所有这些原本寻宝电影中用到快要不行的要素，都被朱延平做成了一锅毫无特色，没放任何作料的东北乱炖，胡乱拼凑在一起，然后告诉大家――这就是一部寻宝的电影了！~我的妈呀！简直太刺激啦~<br />
　　<br />
　　我泣不成声地跪求各位，别再为这部电影共享任何票房啦！盗版它除了破坏环境浪费资源外也只余下虚度光阴这点作用啦！我真的很恨！既恨自己不争气，非要忍到看完，以为会有意外的奇迹发生，却发现和所有现实中的感情一样，从开始就平淡无奇，到最后结束也没有一点火花！又很恨自己没有郭德纲的才华妙笔，不能像他夸奖宋祖德那样好好夸奖朱延平，好好夸奖制片人和投资人，好好夸奖代理百度、诺基亚、起点中文网、盛大文学网……这些品牌的4A广告公司，不能问候那些为了把观众骗进电影院的昧着良心宣传这部烂片的新闻媒体！我好恨我自己，从骨子里就那么贱，非要跑到电影院去给这样的烂片捐钱捐票房，非要贱到被别人当成**，一遍一遍地对他们失望，却还是相信会有奇迹发生！可是我忘了，他们不是为了我们才去拍的电影，他们是为了广告商，为了投资人，为了从像我这样傻头傻脑的老百姓口袋里往外掏银子才去拍的，他们口口声声为了迎合市场，其实只不过是投机炒短线，根本不管观众会不会受内伤！<br />
　　<br />
　　我对所有演员并没有任何恶意，林志玲，周杰伦，陈道明，曾志伟，黄一飞，苗圃，你们也是给人打工的，也是靠自己赚钱的。你们只是被某几个人利用而已，你们是工具，你们真可怜。你们的粉丝更可怜。<br />
　　<br />
　　朱延平，我！刺！你！妈！的！陵！</div>

							
							
							
							
