																											<div id="postmessage_373443" class="t_msgfont">　　“封杀”媒体挽救不了票房颓势<br />
　　来源:羊城晚报　　发表时间：2009-12-07 14:56:41　<br />
　　<br />
　　■林如敏<br />
　　前些天，《刺陵》在京组织媒体试映场，本报记者前往参加。试映结束，记者反映：这是一部烂片。编辑和记者做了充分的沟通，叮嘱记者一定要实事求是，有一说一，在这样的前提下，编发了《&lt;刺陵&gt;剧情漏洞百出》一稿（见12月4日本报B2版），大意是说该片剧情勉强，人物关系混乱，情节设置不合理，但同时也肯定主演周杰伦和林志玲都有相当的可取之处。<br />
　　<br />
　　这下不得了了。《刺陵》前日在京举行首映式，原已接获邀请的本报记者到现场后被拒之门外。有关人员表示，刚接到“上头”通知，不许《羊城晚报》记者参加。有关人员还透露，“上头”对本报之前的报道非常恼火，大发雷霆。“上头”毅然决然地对本报采取了“封杀”手段。<br />
　　<br />
　　对此次“被封杀”，我们只是感到滑稽，还有不解，愤怒谈不上。现在电影产业非常繁荣，媒体在电影方面的报道从年头贯穿到年尾,说不上居功至伟,至少算得上“贡献了一份力量”。但如果都像《刺陵》片方如此这般，片无论大小，剧不论好坏，都只能赞歌一片，那么，这是误导读者,首先是对读者不负责任，媒体的公信力亦将不复存焉；其次，这对于整个电影产业的发展，也是有百害而无一利的，没有批评，如何进步？颂歌与谀词，如果这是当下中国电影人所要的，那未免也太浅薄了点。<br />
　　<br />
　　当然，站在具体某一个片子的角度，他们害怕批评的心情可以理解，尤其在今年这个号称“史上最强贺岁档”的时刻。几十部新片接二连三上市，但消费力的提升是渐进式的，和新片“狂飙突进”式的进展无法构成正比。也就是说，市场票房蛋糕相对固定，你多咬了两口，就意味着其他片子少了两口，所谓僧多粥少是也。况且现在的电影，投资动辄几个亿，少的也有数千万，银幕光影的背后，是血淋淋的商战和利益，能不紧张吗？<br />
　　<br />
　　我们充分理解这种战战兢兢、如履薄冰的心情。但如何从市场竞争中胜出？因素有很多，核心因素却始终只有一条：靠影片质量。越之西子,善毁者不能闭其美；齐之无盐,善美者不能掩其丑。如果真是一部优秀的电影，如果自己的内心有足够的底气，哪会怕别人说三道四？<br />
　　<br />
　　片方还可以说:记者的看法是主观的，是不对的。诚然,就一部电影的观影感受而言，记者的所谓客观也只能是“主观客观”，更多地忠实于个人的感受，同时兼顾和其他观影者的交流。从这个意义上说，一部电影的好坏，的确见仁见智。但一部电影，如果脆弱到承受不起一点批评的声音，那么它的票房崩盘也就是意料中的事。<br />
　　<br />
　　电影业目前看上去很美。但在繁荣的背后，也暴露出了不少弊端，有些是产业结构层面的，有些是从业人员方面的。前些时，电影圈爆出了《阿童木》虚报高票房的丑闻，现如今又有了《刺陵》经不起挑刺要封杀媒体的桥段。“虚报票房”一事刺激了圈内人士，于是有了八部贺岁片签署“自律公约”拒绝假票房的新闻―――说老实话，我很怀疑这个“自律公约”的约束力。经验告诉我们，但凡用自律而不是用制度监控的事，很多是靠不住的。而关于“封杀”，则无需成立什么“反封杀联盟”―――在网络时代，希望通过封杀一家或几家媒体来遏制批评的声音，这不是痴人说梦吗？</div>

							
							
							
							
