													<h2>韩寒：这些狗真麻烦</h2>
						
						
																											<div id="postmessage_339341" class="t_msgfont">今日，闵行区的潘女士家房子被强制拆迁，潘女士在三楼投掷自制燃烧弹，无奈家庭作坊做的燃烧弹只能用瓶子，技术上自然就遇到了瓶颈，威力太小，被消防车轻松扑灭以后，消防车的高压水枪对准潘女士扫射，最终潘女士缴械投降。<br />
看到这则新闻，我欣慰的感到，经过了二十年的发展，社会进步了。冲锋枪换成了高压水枪。那么，为什么会导致如此惨烈的抗衡呢？因为这次大虹桥的建设，潘女士家的面积有480米，而政府只愿意赔偿67万，也就是每平方米761元的房屋重置补贴和1480元的土地补偿。<br />
当这个城市的商品房均价是在用万衡量的时候，强制拆迁的价格还在用百来计算，这就是居民投掷燃烧弹的原因，也就是说，人家本来住了480平方，你要征用人家的土地进行所谓的建设，也就是做生意，你赔偿人家的钱只够人家买40个平方，然后强拆队就来了，换做任何一个有武器的人都忍不住得掏武器。<br />
 <br />
当然，这个例子说明了一些问题，第一是中国政府当年禁枪是有道理的，我记得我很小时候我家里是有一把气枪的，用于打鸟，后来突然有一天政府突然下令说所有的气枪猎枪都必须上缴。这说明我们的政府是有远见的，他意识到了在十几年后，社会矛盾将会加剧，届时如果老百姓配备了气枪，那政府拆迁部门只能配备火箭炮了。<br />
第二是中国政府当年的土地公有制是有远见的，连丝毫没有经济头脑的毛泽东也意识到了，政府吃喝玩乐成本会很大，光靠收税和资源能源垄断弄不好还不够花，土地将是一笔大收入。后来，领导们又担心土地在自己手里卖光了，导致党儿子党孙子们没有地可以卖，到时候自己就成了罪人，所以又规定，土地转让的年限是70年，以方便让孙子们再卖一次。<br />
第三是肯定是政府很后悔的一个问题，早知道现在城市化进程这么有利可图，当时就不应该让农民们有宅基地和自己的房子，导致了现在很多的拆迁问题，想当年在建造监狱的时候，应该利用监狱的图纸顺便也给农民们把自己的村庄建好，一个村一个监狱，一户人家一个牢房，再利用人民日报灌输一下理念，说这就是社会主义新农村，从此自己不用再花钱建房，政府直接送房给大家，家家户户都是水泥混凝土，门直接就是用钢筋做的。当然，钥匙还是要给人家的。这样操作的好处之一是虽然前期花了一些成本，但是后期再也没有拆迁的苦恼。好处之二是万一谁犯了罪，直接给丫钥匙没收了就行了。<br />
 <br />
这个事件中还有几个亮点，就是闵行区一些领导的言论。总所周知，闵行区的领导总是一不小心就把真话给说出来了，我认为这个其实是值得鼓励的，因为他们坦率的真情流露，总是我嘴说我心，比起那些面上一套私下一套的官员至少要强多了。比如闵行区执法大队队长之前就钓鱼事件发表的言论说“没有利益驱动，为什么要帮你”。这句话的深刻与坦诚，只有郑州官员的“你到底是代表党，还是代表人民？”可以媲美。<br />
这次闵行区领导的真心话大冒险接力接到了华漕镇。<br />
华漕镇副镇长高宝金说：你跟政府对抗，那肯定触犯了法律，那肯定要处理的。<br />
另外，建设公司委托给区政府的征地款是每亩地130万元，整个虹桥机场的拆迁总费用高达148亿元。但是政府补贴到农民手中的征地款是每亩地38万元。那么其中的差价为什么就归当地政府了呢？<br />
上海市闵行区交通建设委员会的主任，闵行区动迁指挥部的一把手吴仲权的观点就比较新颖，他认为，闵行区虹桥枢纽这个地块，是在政府的改扩建消息出来以后才大幅提升的，因此由之获得的土地增值价值也不应该由群众取得。<br />
 <br />
你是不是觉得闵行区很可恶呢？你是不是奇怪为什么他们的官员位置还那么稳呢？如果你这么想，你就太嫩了，因为他们是上海市政府的得力干将。这就好比你是公司的部门经理，你要买一个市场价是1000的打印机，于是你给了你的一个员工1000元整，结果你的员工花了300块钱就把这个打印机给强行买来了，还给你开了一张1000的发票，又给了你400，他自己拿走300。不光如此，你还不用负责这个员工的伙食，因为他饿了可以自己钓鱼吃。这个员工唯一的问题是开车赶路的时候压死的几条狗，导致你的办公室外面经常有一堆狗对着你吼，你说，你会不会开除这个员工呢？当然不会。你只会想，这些狗真麻烦。<br />
