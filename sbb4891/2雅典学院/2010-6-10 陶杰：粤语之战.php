&nbsp; &nbsp; 广东人不是日本人，不是英国人，是人种纯净的华人，广州话，也是中国话的一种。所谓「中央」，以打击「地方势力」为理由，掩盖的是北方人歧视粤人的历史仇恨。这股恨意，原出于中国人喜欢自相残杀的 DNA，没有理性的解释。<br />
&nbsp; &nbsp; 广东一向是鱼米乡，广州人传统上活得比河北人好。广东菜清淡精致，花样繁多，不像北佬只 面食为主。广州话保存宋代的词汇发音，历史考证，比他古远。广东岭南派的水墨，令中国画弘扬海外。广东华侨在南洋生意做得好。凡此种种，总之你比他好，其它中国人天生的妒恨，看不过眼，之火就会在咆哮中，熊熊燃烧。<br />
&nbsp; &nbsp; 广州话令北佬眼红，因为北方人的语言智商相对低，他们也不明白广府话的一些层次细腻的情感─譬如，广州话里，「折堕」这个形容词。<br />
&nbsp; &nbsp; 什么叫折堕？一个富家小孩，浪费米粮，吃饭剩半碗，长辈会骂他折堕。<br />
&nbsp; &nbsp; 同时，一个富豪破了产，沦为劳工，旁观者也觉得他折堕。广东话的折堕，意思是：「在风光时高高在上，不会积阴德，权力使尽，有一天，福尽运转，沦为地上任人践踏的泥巴。」<br />
&nbsp; &nbsp; 只两个字，要这样才解释清楚。折堕隐隐有宿命果报之意。广东人的宿命观很含蓄，因为顺德番禺、佛山南海一带，水田阡陌，富庶一片，「折堕」隐含的宿命观，眼看他起高楼，眼看他宴宾客，是广东人在这样的地理环境，于一株榕树下，提着一把扇子，悠然旁观世态而泛生的细腻的哀思。<br />
&nbsp; &nbsp; 折堕这个名词，本身就有广东人食海鲜清蒸的味道，讲精致的口感，北京人和上海人，都没有广州人这一份通达，折堕一词，译为英文， suffering已太嫌粗糙，而且只得其中一义，不得其余。<br />
&nbsp; &nbsp; 广东人与北方的冲突，语言是一个战场。广东人不说「瓶子」，说「樽」，在宋词里：「暂停征棹，聊共引离樽」。在 IT世代，用 iPhone传讯，当然是一个「樽」字比「瓶子」省事，北方人知道了，益发妒火中烧。<br />
