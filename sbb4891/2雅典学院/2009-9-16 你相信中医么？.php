																											<div id="postmessage_242762" class="t_msgfont">我个人很反对中医，认为中医从根本上就是伪科学的。经历了这么多年，肯定能碰巧治一两种病，但是我们需要的是科学的证据，你做个实验证明其有效啊，事实是，几乎没人敢做实验，因为很多都无效。中医从理论上就是站不住脚的，什么阴阳五行，现在看看和算卦没有什么区别。<br />
&nbsp; &nbsp; 最近的猪流感，又有学校弄什么中药，我觉得，中药除了能让一部分人赚钱之外，没有任何意义。最近学校军训，给学生发放板蓝根，我看了很不爽，那种黑乎乎的东西，有什么效果，你们做过双盲对照试验么？我们吃的每一个正规西药都是做了无数次试验之后才进入百姓的口中的。<br />
&nbsp; &nbsp; 中医常常说，某某某地的某某某人的了什么绝症、怪病之类的，给治好了，而且一般是偏远地区，这就很不靠谱。<br />
&nbsp; &nbsp; 西药会对大多数人有效，当然不排除有过敏的，有不适应的，这就是科学的态度。<br />
&nbsp; &nbsp; 对于方舟子，我个人是很肯定的，他不靠打假盈利，而坚持不懈的打假。在学术方面，确实起到了很好的作用，很多大学迫于舆论压力，确实处理了一批人。<br />
&nbsp; &nbsp; 方舟子对于中医的看法和他的做法，我觉得都是可以肯定的。<br />
&nbsp; &nbsp; 当然，公众人物，被骂是正常的，但是我希望，骂他的，可以举出具体的例子，我不是道德上有洁癖的人，他可能会有缺点，但是这却不能掩盖他其他方面的成就。他的确打了一些不是假的假，但是他的目的都是好的，在学术上，我觉得怀疑精神是可贵的，打假大错了，之后澄清了，这无伤大雅。但是，因为怕大错了，而错过一个真的“假”，这样可就不好了。<br />
&nbsp; &nbsp; 我是一个学科学的，我尽力做到客观，当然，我才智平庸，天资愚钝，很可能做不到客观，希望大家指点。<br />
&nbsp; &nbsp; 今天就想了这么多，希望可以和大家讨论一下。<br />
&nbsp; &nbsp; 我想听听大家的看法。<br />
