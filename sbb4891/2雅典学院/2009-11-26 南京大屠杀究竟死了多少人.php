中国人特喜欢装B，比如12月13日， 肯定又要大张旗鼓的纪念南京大屠杀七十周年，就好象一个花容枯槁的老奶奶，一把眼泪一把鼻涕的哭诉：70年前我被邻居家12岁的小毛头强奸了一百遍啊一百 遍……整天念叨个没完。问曰：真的有一百遍？你一遍遍数的？老大妈立刻怒道：你这个汉奸走狗，竟然想为强奸犯翻案……<br />
