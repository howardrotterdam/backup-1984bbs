													<h2>南京大屠杀究竟死了多少人</h2>
						
						
																											<div id="postmessage_343705" class="t_msgfont"><a href="http://www.bullock.cn/blogs/naoko/archives/87462.aspx" target="_blank">http://www.bullock.cn/blogs/naoko/archives/87462.aspx</a> <br />
（作者：Anonymous）<br />
中国人特喜欢装B，比如12月13日，肯定又要大张旗鼓的纪念南京大屠杀七十周年，就好象一个花容枯槁的老奶奶，一把眼泪一把鼻涕的哭诉：70年前我被邻居家12岁的小毛头强奸了一百遍啊一百遍……整天念叨个没完。问曰：真的有一百遍？你一遍遍数的？老大妈立刻怒道：你这个汉奸走狗，竟然想为强奸犯翻案……<br />
<br />
同样，我现在要是问“三十万”这个数字是怎么来的，我保证，那些整天作一脸无奈状说“中国人越来越麻木了才过了几十年就连国耻都忘了”这种话的人，十个有九个都答不上来。但是这不妨碍他们装B啊，一到这天，又有人开始长吁短叹：还有多少人记得这个日子……<br />
<br />
“南京大屠杀”这个概念，最早出现东京审判上，当时中国送交了30万的数字，但最终结果是认了20万。为什么今天我们都说是30万呢？话说1985年，那是一个春天，有一位老人……在大屠杀纪念馆题下了30万这个气势恢弘的数字，从此便成定论。从80年代起日本就不断提出要和中方合作调查南京大屠杀死难者的具体人数，都被中方以“没必要”“已经定了”为理由加以拒绝，对于南京大屠杀的死难人数，新中国从来没有进行过任何系统的调查，不是因为调查起来有难度，而是根本就没心思做，30万这个数字随口一胡扯就出来了。这个基本上就是中国人对待国耻的态度，纪念纪念就好了，不要深究，不能深究，一深究就要出问题。<br />
<br />
像我这种业余网民稍微深究一下，30万这个立论就要被打垮。首先看几十年前的那份“30万”初稿。根据国民党政府提供的埋尸记录，其中大票的有世界红十字会43121具，南京红十字会收埋22371人，最大的一笔是慈善机构崇善堂，收埋人数达112266人，占了30万的1/3。问题来了，目前研究大屠杀最重要的资料主要是当年一些人的回忆录，可这些人从来就没提到过崇善堂这个组织，而且这个组织在南京大屠杀之前几年就已经解散了，连中国红十字会会长都说不知道。红十字会还收容了二十万难民，而崇善堂简直就是埋尸专业户，除了埋尸之外什么都没干过，也不知道是谁资助的，一个在南京各地收埋了11万人的大型组织，当年身在南京的人竟然都没听说过？<br />
<br />
最后，这份报告没有被远东国际法庭认可。于是六人委员会四舍五入一下，再考虑到焚尸灭迹，拿张草稿纸这么一估算，20万就拍板定了。事实上把这11万的记录去掉，剩下的19万还是经不起推敲。审判之前一个月，南京《新民报》记者金光群进入国防部采访，负责人向他介绍“六个月来经过调查，收到市民控诉信53件，市参议会也转来了很多材料”，当时金光群还挺高兴，赶紧做了个独家报导。谁知一个月后开庭，呈报的证据竟然多达屠杀2084件、奸淫308件、抢劫暴行116件、焚烧破坏罪行186件。从6个月只收到53封控信，到接下来一个月转眼变成数千件暴行，国民政府工作效率之高，连崇善堂都难以比肩！（事实上我很怀疑崇善堂的埋尸记录就是这时候造出来的）其中第一证人鲁苏，更是在“夜间”看到“57418人惨遭杀害”，所有证人加起来，一共看到“353026人被害”，我看到这里都忍不住热泪盈眶，亩产万斤的根总算找到了啊！<br />
<br />
南京有没有这么多人给日军杀呢？魏特琳日记里说“据说还有二十万人留在了城里”（11月30日），拉贝则在10月17日的一封信中提到：“如果他听说过大约100万～120万居民中至少已有80万人离开了这个城市，那他对现在城里到处是死一般的寂静和几乎空荡荡的街道和广场就不再会感到惊讶了。”拉贝是当年国际安全区的负责人，这个安全区占了南京面积1/3，内有难民20万，当年提供了多少人道救助可想而知，再加上安全区外日本人统计出的5万人口，基本符合当年当事人记叙的情况。只有这么点人口还都在安全区里，哪儿来的30万给人屠？当年被杀的30万中国人难道都是SB，南京城1/3那么大的安全区，不会往那里跑？中国在80年代以前对这个安全区还知之甚少，我小时候还看过一部电影《南京屠城》，讲的就是日军如何秘密攻入安全区，安全区内的中国勇士如何把这个消息传到外界，拍得跟三级片差不多，基本上就是扯淡。当年安全区内各负责人都提到过“有时遭到三五人的小股日军骚扰”，但从来没有说是大规模攻入安全区杀人放火的，波黑内战波及安全区，马上惹怒了美国佬，轻松就把两边都给平了，攻打安全区，你借日本人一个胆儿？中国人很弱智的一个地方，就是老是喜欢搞些水准特低的YY，自己还停留在16世纪，却总以为别人都跟他一样，你YY要是能强身，那还要手干什么？前几年那个东史朗案就特有意思，明明是个民事纠纷，非要给中国政府上升到政治高度，你当人家的法院都跟鼓楼区人民常理法院一样，想操纵就操纵啊？<br />
<br />
民间学者读到这些外国人写的资料后就开始生疑了，连人都没有，怎么杀？根据远东国际法庭对南京大屠杀的定义，是“发生在南京城内（明城墙）及附近地区的一系列暴行”，似乎可以解释被杀的30万人是哪里来的，问题是附近到哪里？是城郊还是苏锡常镇？整个江苏？显然这里指的是城郊，而且南京城内的屠杀才是重点。如果要覆盖整个江苏甚至上海，那我就不得不联想到轮子了。99年轮子到中南海门口闹事，CCTV没日没夜的说练轮子的人有多少多少自杀，数字看起来挺吓人，但是你除以轮子的总人数再和那个水分足足的全国自杀率比一比，咦？好象人家轮子自杀的还少一点啊……<br />
<br />
人口问题一直是南京大屠杀研究中的一个关键点。孙宅魏从86年一直研究到91年，终于论证出南京当时其实还有60万人。咱姑且不论这个论证是否合理（我也没看过），已经充分暴露出了中国这些大屠杀学者在人口问题上的虚弱本质。人家拉贝大屠杀那年就呆在南京城里，“20万人口”是早就写好的，你过了50年才开始研究，花了5年才得出结果，那之前50年你连人口问题都解决不了，凭什么说死了30万人，你不是胡扯吗？你不去研究南京大屠杀死了多少人，而是研究南京当时有多少人，你TM不是闲得蛋疼吗？<br />
