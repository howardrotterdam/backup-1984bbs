这就要说说美国上世纪末一个极为经典的案件――辛普森杀妻案，当年闹得满城风雨，我相信大家就算不了解至少也听说过。为什么在罪证确凿的情况下，辛普森却被无罪释放？LA警察采证时，违反了正规程序，比如什么私闯民宅搜集证据啊，这使得辛普森的律师团有理由认为，那些证据有可能是“办案警察联合起来陷害辛普森”。而关键证人LA警察福尔曼的证词，对辛普森一案的判决有决定性的作用，辛普森的律师问他是否有使用过侮辱黑人的词语，（由于辛普森是黑人，当时民众普遍认为律师是在打种族牌，寻找LA警察陷害他的动机，因此也十分不满，就像今天中国人不满少数民族保护政策一样），福尔曼说没有，律师却出示了一份录音，证明福尔曼确实曾经大量使用侮辱黑人的词汇。这件事的意义不在于LA警察是否有陷害辛普森的动机，而是证明福尔曼这个人有品格缺陷，竟然在法庭上公然撒谎，凡是他提供的证据，法庭全部不予参考，而最后的结果更是让人大跌眼镜，辛普森无罪释放，福尔曼3年徒刑！<br />
<br />
诚然，美国之所以严格遵循“程序正义”，是出于他们宪法中“有限政府”尤其是限制警察这种暴力机构行为能力的一贯精神，而个人行为也不能和国家行为相提并论，不会说中国在南京大屠杀这件事情上撒了谎，日本就被判处无罪了。但是影响是相同的，中国人老说日本人不如德国人，认错态度不彻底，那都是放P，你整天造谣说别人干了这个干了那个，它能不跟你急？就凭你这点华南虎水平的造假能力，就想逼人认罪，真当人家脑袋烧坏了？中国人只知道右翼分子，却不知道日本其实也有洞富雄这样的“大屠杀派”，他们对于大屠杀的研究远远超过中国那些狗屁专家学者，写的书一车一车，但是张纯如那本书一面世，大屠杀派都受不了了，《南京暴行》最后在日本没有出版，不是如中国FQ造谣的那样是右翼分子阻挠，恰恰是张纯如和大屠杀派产生了纠葛。大屠杀派认为这本书硬伤太多，要出版的话可以不作修改，但必须和他们修订的一本小册子捆绑销售，但张纯如不依啊，她书里序章就写在日本研究大屠杀是要掉脑袋的（可见弱智到什么程度），大屠杀派在她眼里就是挂羊头卖狗肉，说来说去还是军国分子，什么硬伤太多，不就是想否定南京大屠杀？当下断然拒绝，于是日本又给国内的FQ们捞到把柄了。而日本右翼团体简直是捡到宝了，逮着这本书狂批，其实两边论战经常是这样的，大屠杀派说：据我们最新研究发现，某某证据可以证明大屠杀的存在，理由12345……然后右翼说：据我们最新研究发现，中国的某某证据是伪造的，理由12345……谁也批不了谁，自说自划，可听众的倾向性是很明显的，他们更喜欢那种揭穿谎言的内容，等日本的年轻人听多了之后就开始琢磨了，中国伪造了这么多证据，会不会这件事根本就没发生过……？再说了，你一国政府都能下三滥到如此地步，整天在国内甚至国际上散布谎言，想想什么印花税啊亩产万斤啊之类的就一点也奇怪了，还说要建立信用体制，你TM自己信用最差，撒的谎最多，还整天说中国人素质低没信用，不是贼喊捉贼吗？<br />
<br />
