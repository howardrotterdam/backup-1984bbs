我讲这么一堆，意思只有一个，日军是很 凶残，但哪国军队不凶残？解放军不凶残？美军不凶残？我看论凶残，红卫兵才是排第一，张志新临刑前还想说点什么唤醒民众，毛远新就示意把她喉咙割了，小红 兵看着一个弱女子的鲜血汩汩流出，欢呼阵阵，这个场面我想想都骨髓发颤。大家都一样，日军不会比其它军队更残忍一个等级，战争就是这么一回事，打到最后都 是丧失人性，中国人为什么整天要渲染日军和其它军队比有多么多么残暴呢？如果不把日本说得太坏，那中国人反日就显得太没格调了，别人打你一下，你就整天反 它，这不就是冤冤相报，气量狭隘吗？但是日本这个国家要是很变态，那就另当别论了，我们反你，因为你根本不是人，是畜生，把你从地球上消户是我们正常人的 责任。但说到底就是复仇心作祟罢了，别人打我一拳，我一定要再踢他一脚，格调就是这么低，还造谣生非，把对方描述得多么多么恶劣以宣扬自己的正义性，典型 的当婊子还要立牌坊，更是下作到极点。看看世界上以仇恨作卖点的国家，都是些什么货色？二战都是国际上审判过的事情，中国人口头上尊重法庭宣判的结果，心 里面都想来个私设刑堂，一个国家就这种素质，还想搞法治，搞和谐，这不是扯淡吗？<br />
<br />
把确定是伪造的证据去掉，把常规战争行 为的死难者去掉，再数一数，大概还有多少人？我估计想上六位数都非常困难，可能真如一些安全区内的人所说，平民遇害只有四万人。但这个事实是很难查证的， 因为时间过得太久，像犹太人那样统计已经不可能了，新中国成立快60年，从来没想去调查过这个问题，等到80年代才旧事重提，兴风作浪，这时候还真是死无 对证，查起来有相当难度。但国民政府伪造了30万这个数字，这是绝对跑不了的。一般说到这里，必然会出现一种傻B，他会非常愤慨的对你说，杀30万叫杀， 杀10万不照样叫杀？我太欣赏这种人的精神了，简直就是活二五，既然杀10万也叫杀，你他妈的干吗要信口开河，说句实话就会死？<br />
<br />
这就要说说美国上世纪末一个极为经典的 案件――辛普森杀妻案，当年闹得满城风雨，我相信大家就算不了解至少也听说过。为什么在罪证确凿的情况下，辛普森却被无罪释放？LA警察采证时，违反了正 规程序，比如什么私闯民宅搜集证据啊，这使得辛普森的律师团有理由认为，那些证据有可能是“办案警察联合起来陷害辛普森”。而关键证人LA警察福尔曼的证 词，对辛普森一案的判决有决定性的作用，辛普森的律师问他是否有使用过侮辱黑人的词语，（由于辛普森是黑人，当时民众普遍认为律师是在打种族牌，寻找LA 警察陷害他的动机，因此也十分不满，就像今天中国人不满少数民族保护政策一样），福尔曼说没有，律师却出示了一份录音，证明福尔曼确实曾经大量使用侮辱黑 人的词汇。这件事的意义不在于LA警察是否有陷害辛普森的动机，而是证明福尔曼这个人有品格缺陷，竟然在法庭上公然撒谎，凡是他提供的证据，法庭全部不予 参考，而最后的结果更是让人大跌眼镜，辛普森无罪释放，福尔曼3年徒刑！<br />
<br />
诚然，美国之所以严格遵循“程序正 义”，是出于他们宪法中“有限政府”尤其是限制警察这种暴力机构行为能力的一贯精神，而个人行为也不能和国家行为相提并论，不会说中国在南京大屠杀这件事 情上撒了谎，日本就被判处无罪了。但是影响是相同的，中国人老说日本人不如德国人，认错态度不彻底，那都是放P，你整天造谣说别人干了这个干了那个，它能 不跟你急？就凭你这点华南虎水平的造假能力，就想逼人认罪，真当人家脑袋烧坏了？中国人只知道右翼分子，却不知道日本其实也有洞富雄这样的“大屠杀派”， 他们对于大屠杀的研究远远超过中国那些狗屁专家学者，写的书一车一车，但是张纯如那本书一面世，大屠杀派都受不了了，《南京暴行》最后在日本没有出版，不 是如中国FQ造谣的那样是右翼分子阻挠，恰恰是张纯如和大屠杀派产生了纠葛。大屠杀派认为这本书硬伤太多，要出版的话可以不作修改，但必须和他们修订的一 本小册子捆绑销售，但张纯如不依啊，她书里序章就写在日本研究大屠杀是要掉脑袋的（可见弱智到什么程度），大屠杀派在她眼里就是挂羊头卖狗肉，说来说去还 是军国分子，什么硬伤太多，不就是想否定南京大屠杀？当下断然拒绝，于是日本又给国内的FQ们捞到把柄了。而日本右翼团体简直是捡到宝了，逮着这本书狂 批，其实两边论战经常是这样的，大屠杀派说：据我们最新研究发现，某某证据可以证明大屠杀的存在，理由12345……然后右翼说：据我们最新研究发现，中 国的某某证据是伪造的，理由12345……谁也批不了谁，自说自划，可听众的倾向性是很明显的，他们更喜欢那种揭穿谎言的内容，等日本的年轻人听多了之后 就开始琢磨了，中国伪造了这么多证据，会不会这件事根本就没发生过……？再说了，你一国政府都能下三滥到如此地步，整天在国内甚至国际上散布谎言，想想什 么印花税啊亩产万斤啊之类的就一点也奇怪了，还说要建立信用体制，你TM自己信用最差，撒的谎最多，还整天说中国人素质低没信用，不是贼喊捉贼吗？<br />
<br />
中国人要是有犹太人那种精神，日本现在 也没那么多屁可放了，出了这种事别怪人家，好好想想你自己干不干净。日本对中国真可以说是仁至义尽，先说这个战争赔款问题，当年毛蒋都觉得自己该分一杯 羹，整天吵个不休，最后列强烦了，把搅局的中台都踢到一边和日本私下解决。蒋率先放弃了索赔权，以前说是为了换取外交地位，近几年研究说可能是老美逼压 的，真相不明，不管它；而老毛随后也放弃了战争赔款，条件是日本认中国不认台湾。日本人马上开开心心的照办了，但人家外交辞令写得也是滑溜滑溜，给日台之 间留下了足够的后路。老毛一贯慷人民之慨，这个咱就不说了，都审丑疲劳了。以后别说日本人没给赔偿，你自己贱，能怪谁？中国最搞笑的外交路线就是逼着这个 承认一个中国逼着那个承认一个中国，谁不承认就在联合国搞谁。如果台湾真的是中国的一部分，干吗还要别人承认，不是脑有病吗？台湾人给自己政府纳税，拿自 己国家的护照，事实上早独立几十年了，承认有个P用？中国逼人家承认，人家一般都照办，无所谓呗，中国人傻，钱多，速来，好地方，谁跟钱过不去啊，无非是 口头应付一下。这方面李奇微的分析我一直觉得最为透彻，中国这套SB外交路线不是给老外看的，是给中国人民看的，GCD谈判一般不是求实利，而是求面子， 只要回去能忽悠本国人民，能维护安定团结社会主义初级阶段一百年不变，那什么都好说，割几块地给几块钱，败败老祖宗留下的财产，那算个P哦。<br />
<br />
虽然日本没给中国人赔偿，但是从72年 到现在，一直以经济援助的方式间接补偿中国，中国是日本最大的对外援助国，日本则是中国最大的援助资金来源，30多年来无偿援助和低息贷款加起来有3兆日 圆，低息贷款利率之低，考虑到通胀等因素，基本上就是无息的，这么一大笔钱，帮中国度过了最艰难的岁月，其中有不少都是直接援助农村的基础建设项目，像什 么水电教育，都是国内农村最缺的东西。国人是怎么做的呢？安徽阜阳建起一座豪华办公大楼，可当地农民却找日本要援助建小学校，日本来考察时看到那座办公大 楼，连称“不理解”，当然不理解，人家活在21世纪，哪能理解你这种史前文明？可不理解归不理解，最后钱还是给了；日本人松冈洋明捐献中国教育7000 万，被地方政府全部克扣，打官司追讨五年，仍然无果；中国人抵制日货抵制得那么HIGH，可在中国因隐瞒疫情差点成为文明世界公敌的SARS期间，日企还 无偿支援了16亿；中国人说日本篡改历史教科书，可那版教科书全日本只有0.4%的学生在使用，而中国所有的历史教科书都在撒谎；中国人说日本人不道歉， 说小泉纯一朗参拜神社，却不知道日本首相72年就已经书面道歉，历代有8位首相口头向中国领导表示了歉意。这些事情国人都不知道，他们就知道什么钓鱼岛， 人家台湾渔民的地盘，GCD都不吭声，你大陆小FQ瞎叫个什么劲，是不是建国后割给印度菲律宾俄罗斯朝鲜缅甸越南蒙古塔吉克斯坦印度尼西亚马来西亚文莱的 领土领海小岛什么的已经全部要回来了？<br />
<br />
还要怎样，还能怎样？国内反日的这帮 人，说真的，不是无知就是无耻，年纪小无知就算了，可还有这么一种混球，往往顶着个不知道在哪个村的网吧里拍的视频照当头像，我讲了这么一长串，他不仅毫 无悔改之心，还感觉我既侮辱了他的人格又侮辱了他的智商，他会理直气壮的放声高叫：妈的，日本人援助我们，那都是天经地义的！这种人我就不想多说了，没素 质没人格没智商，正所谓水至清则无鱼，人至贱则无敌，想侮辱都侮辱不起来。日本人对中国这种情况当然也很了解，援助了30年，得到的不是友好，而是忘恩负 义，舆论自然也很不满。从明年开始，日本不会再向中国提供任何经济援助，我得知此事真是由衷的感到欣慰，日本人SB了30年终于开窍了，国内这帮FQ是伺 候不好的，有那么多钱还不如养一条狗呢。但是我们国家都很会给自己找台阶下，说是中国强大了，没必要再拿援助了。都TM空投农民超远距离采矿了，还非要 说，出了一点小意外，情况还在我掌握之中，简直没治了。<br />
<br />
所以说，什么南京大屠杀，我看还是别纪 念了，连死了多少人都搞不清，纪念个毛，这不是装B吗？死了这么多人，确实很屈辱，但真要说屈辱，南京大屠杀就算死300万个人，还是永远也排不上号！什 么叫屈辱，一个国家被杀死这么多人，连个像样的统计都懒得做，这叫屈辱；一个国家能将政府的罪责和三千万人命推脱为自然灾害，这叫屈辱；一个国家有着荒唐 到让人无法理解的十年动乱，如今却没有人为此负责，没有人敢公正的评价历史，这叫屈辱；一个国家用坦克碾碎民主的呼声，把枪口对准几千个大学生，这叫屈 辱；一个国家的人民才短短二十年不到就把自己屈辱的历史都忘光了，只记得个日本，这才叫屈辱！相比之下，遗忘了南京大屠杀又怎么样？记住了南京大屠杀，你 以为自己又真记住了什么？</div>

							
							
							
							
