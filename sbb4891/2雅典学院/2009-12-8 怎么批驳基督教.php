													<h2>怎么批驳基督教</h2>
						
						
																											<div id="postmessage_362623" class="t_msgfont">一般而言基督教也不是什么特别恶劣的存在。不过，基督徒和受基督教影响太深的人，往往会对其它文明文化嗤之以鼻。其实，他们的意思不过是说：你们没信基督教，所以活该倒霉。这也没什么，老王卖瓜自卖自夸呗，可以理解。但是，大部分情况下他们说得并不是这么直白，这就导致误解了：等于是把水搅浑。而把水搅浑这种事，则从来都对那些不讲理的人（这里指的不是基督徒）更有利。<br />
所以，有时候看到苗头不对，我们就应该主动出击，批驳基督教，让这些胡搅蛮缠的人先名声扫地。<br />
批驳基督教的要诀在于：不要质疑上帝是否存在。佛教是不承认上帝存在的，无神论也是如此。但是，不管你是信什么的，质疑上帝存在都比较不讨好。因为基督徒是很狡猾的，他们善于变换上帝的定义（谁认为上帝存在，谁就更有权诠释上帝），如果他们把上帝定义成自然神（即某种意义上的大自然），那上帝又怎么可能不存在呢？<br />
基督徒的狡猾之处就在于，当你质疑上帝的存在的时候，他们就用一个自然神来对付你；当你不质疑上帝的存在的时候，他们又要搬出基督教的具体教义来”教导“你。所以，我们批驳基督教的时候，要根据这样一种思路：<br />
上帝是可以存在的，但是，任何基督教的具体教义，都是没有来路的。任何人都不能想象大自然会用基督教的具体教义来教导人类。基督徒一般是把《圣经》或对《圣经》的理解作为基督教教义的来源。但《圣经》的可靠性，除了他们自己以外，是没有任何正常人会认可的；至于他们对《圣经》的理解，几千年来都是在变化的，更没什么神圣性可言。<br />
总之，宗教信仰要求虔诚，但是，对这么古老的事物的虔诚信仰是再荒谬不过的了。而如果宗教信仰不虔诚，那他们实际上也谈不上基督徒（《圣经》和基督教历史上有很多对不虔诚者的大力批判），不过是拿基督教当幌子的人群罢了。这种不虔诚的信仰对基督教以外的人而言，虽然谈不上荒谬（人们总要信点什么），但是拿基督教当幌子这种行为，却是失去诚信的表现，只能让自己名声扫地。</div>

							
							
							
							
