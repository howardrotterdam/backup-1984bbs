在独[和]裁政治的迷宫中寻找真相的女性<br />
菲利普・P・潘<br />
华盛顿邮报国际部<br />
2004/12/18 星期六；Page A01<br />
<br />
不锈钢老鼠译<br />
<br />
北京--在网上，她管自己叫不锈钢老鼠，一个美国系列科幻小说中的传奇英雄。但爬上北京破败的南城中一所破旧住宅楼那灯光昏暗的楼梯后，刘-荻看上去更像一只胆怯的老鼠。<br />
&nbsp; &nbsp; “我想就是这里，”这个戴着椭圆形眼镜的矮小女性停在一扇门牌号为407的铁门前，轻声说。“我想就是这里，但不能肯定。”<br />
&nbsp; &nbsp; 两年前，警察以颠覆政府的罪名逮捕了现年24岁的刘，一年前，由于国际上的呼吁和中国网民的强烈声援，政府释放了她。当时刘是一位大四学生，她的许多粉丝认为她是由于写文章嘲弄当权的共*党并且将其贴到网上而被投入监狱的。<br />
&nbsp; &nbsp; 但刘不那么肯定。有两个问题在折磨着她：她的一个朋友是政府的线人吗？是他陷害了她吗？<br />
&nbsp; &nbsp; 几个月来，她在调查自己被捕的事件，进行得很缓慢，她怕如果自己挖得太深的话，当局会做些什么。有时她担心自己太多疑了。更多的时候她确信自己受骗了。但很难找到有力的证据，而那个朋友似乎也失踪了。<br />
&nbsp; &nbsp; 现在她就在他的房间外面。楼道里很暗、很安静，暮色在水泥墙上投下了班驳的影子。刘轻轻地敲门。没人回答。<br />
&nbsp; &nbsp; “还能做什么呢？”在城市傍晚的繁忙交通中慢慢开车回家的路上，她问道。她用尽全部线索，在无孔不入的政府安全机关的阴影下进行的漫长搜寻已经接近结束，却并不比开始的时候更接近真相。<br />
&nbsp; &nbsp; 毛**死后，超过四分之一个世纪过去了，中国共*党建立了世界上最成功的独[和]裁政府，在保持经济快速发展的同时维持对权力的垄断。有时它做得简单粗暴：异议人士一旦跨过了一条清晰的界线，就会被送进监狱。<br />
&nbsp; &nbsp; 但与中国政府之间的冲突往往是任意发生的、不合逻辑的，就像什么出了“圈”一样荒诞不经、难以理解。这在今天尤其真实，因为党努力要使老的社会控制方法迎接维持独[和]裁统治的新挑战，因为这个社会正在谋求并且赢得更大的自由。<br />
&nbsp; &nbsp; 对像刘这样在反复无常的制度掌控之下的个人来说，试图弄清楚发生在自己身上的事，就像穿过一个巨大而恐怖的迷宫，每个角落里都隐藏着怀疑和恐惧。这是关于一个年轻女性与独[和]裁主义的冲突，和她试图面对身后的神秘事件的故事。<br />
<br />
一个害羞的书虫在网络上获得了解放<br />
<br />
&nbsp; &nbsp; 刘第一次登入互联网是在大学二年级，这马上吸引了她。她是个书虫和科幻怪客，矮小并且打扮有些单调，驼背的姿势使她显得更加害羞。她在北京长大，总感到自己不合时宜。但在塞伯空间中，她感到自己解放了。<br />
&nbsp; &nbsp; 为了找一个网名，她想起初中时读过的一个系列小说，关于一个骗子被招募来拯救宇宙的故事，这就是不锈钢老鼠。她身上的叛逆性让她特别喜欢书中的这句话：“我们是保护社会的法网里的老鼠--我们能在他们设置的重重障碍和制定的种种法律之外活动。”<br />
&nbsp; &nbsp; 在互联网上探索的时候，刘注意到了那些发表被党所禁止的文章的网站。可能因为祖母是人民日报--党的机关报--的记者，她一向对政治感兴趣。她喜欢的小说《1984》和《发条橙》揭示了极权主义的荒谬。但是在网上，刘才开始积极撰写起批评中国自身的政治制度的自由主义文章来。<br />
&nbsp; &nbsp; 不久，她参与了互联网上的讨论，关于政*治*改*革和其他被党所禁止的话题。开始，她只是阅读其他人所写的东西，但随后她开始发表自己的文章，并且由于对共产主义统治下生活的荒谬进行了滑稽的讽刺而迅速赢得了声誉。<br />
&nbsp; &nbsp; 在一篇文章中，她大声疾呼声援一位被投入监狱的网站站长，因为他的网站上有政治内容，刘建议全体网民一齐到警察局投案自首。为了确保当局“战果辉煌”，她补充道，“对那些还没有在互联网上发表反动言*论的人，应该劝说其先发表反动言*论（再投案自首）。”<br />
&nbsp; &nbsp; 刘说，她害怕因自己的文章而受到惩罚，但也因在网上受到热烈欢迎而感到鼓舞。她花好几个小时在大学的计算机房里，在互联网聊天室里与粉丝谈话，结交新朋友。她也把和网友的交往延伸到现实世界中。<br />
&nbsp; &nbsp; “比较来说，我在互联网上比在大学中交到的朋友更多，”她说。“当然我在大学里也认识不少人，但是能深交的不多。”<br />
&nbsp; &nbsp; 2002年四月，刘在北京师范大学读心理学的第三年里，她收到一个自称为阳春白雪的网民发来的信息。他说自己认识她的一个网友，这个网友是东北某热力公司干部，名叫姜力钧，刘想起姜曾经提到过阳春白雪一次。<br />
&nbsp; &nbsp; 接下来的几周里，刘经常与阳春白雪聊天，阳春白雪告诉她自己叫李毅兵，自称在北京的一家投资公司工作。他说自己欣赏刘的文章，他们讨论了政治并且交换了一些笑话。有几次，他说想跟刘见面。最后她同意了。<br />
<br />
一个有些危险想法、执着得出奇的朋友<br />
<br />
&nbsp; &nbsp; 五月里的一个凉爽的上午，他们在刘的大学外面见面。他先到，按网上约好的拿着一份报纸，刘马上认出了他：一个瘦高、三十岁左右的小伙子，头发比较长，脸上长着粉刺。<br />
&nbsp; &nbsp; “我想他名字叫阳春白雪，应该长得比较帅，”刘回忆道。“但实际上他没什么特别的。他看上去像一根竹竿一样。”<br />
&nbsp; &nbsp; 他们在一家肯德基里聊天，喝可乐吃薯条，然后在一家中餐馆吃饭。李提到他喜欢远足，并且再次强调了他与姜是朋友。但刘说大部分时间是她自己在说，因为李话说得很少。<br />
&nbsp; &nbsp; 李说他想见刘的其他网友。他看来很好，刘回忆道，因此她在接下去的几星期里给他介绍了几个朋友。过了一段时间，她也把他当成朋友了。<br />
&nbsp; &nbsp; 他看来同意她关于中国需要政治变革的观点；他甚至表现的更激进，她说。一次，他提出搞爆炸。还有一次，他谈到建立一个地下党。<br />
&nbsp; &nbsp; 刘说她认为他的想法很危险，并且告诉了他，但并不很认真看待。他看来像那种说大话但什么也做不成的人。“他给人印象是个野心勃勃但是没什么能力的人，”她回忆道。“他可能是认真的，但我认为他愚蠢可笑，我对他说过几次。”<br />
&nbsp; &nbsp; 他给她看了一份他起草的党纲，她不屑一顾，因为写得太差。但李开始纠缠她，要她帮助修改。刘不愿意，但他一直要求，而她觉得再这样拒绝一个朋友是不礼貌的。她回忆道，那个夏天里，她有两次周末到办公室去见李，在电脑上帮他修改这份文件。<br />
&nbsp; &nbsp; 随后，九月里一个下雨的晚上，李带刘和她的另一个网友吴一然去他的住处。他再一次谈起建立地下党，刘回忆道，说他们共同的朋友姜支持这个主意。他还计划在即将召开的中国党代会期间发出诈弹恐吓。刘和吴大笑起来，但还是警告李不要这么做。<br />
&nbsp; &nbsp; 两个月之后，当学校里的工作人员叫她到学校保卫部去的时候，她几乎已经忘了这次对话。十几个便衣正在等着她。<br />
<br />
审讯和逮捕，以及网络请愿<br />
<br />
&nbsp; &nbsp; 来人没有说明自己的身份，但刘猜他们来自秘密的国家安全部。三个小时里，他们客气地问关于她的文章和网友的问题。她很害怕，试图在回答问题时兜圈子，拖延时间。<br />
&nbsp; &nbsp; 但审问继续下去的时候，刘知道了他们的兴趣主要在李身上。他们看来并不很了解他。他们让她带自己去找李，但她忘记了李的地址。随后他们谢谢她，让她走了。<br />
&nbsp; &nbsp; 刘如释重负地回到宿舍。但她的噩梦才刚刚开始。第二天上午，学校再次叫她到保卫部去。这次，穿制服的北京市公安局警察等在那里。他们只说她涉嫌犯罪，并把她送到秦城监狱，一个因关押政治犯而臭名昭著的地方。<br />
&nbsp; &nbsp; 刘非常害怕--并且很困惑。为什么昨晚国安放了她？为什么他们的对手--公安现在卷进来了？当公安开始审讯她的时候，疑问加深了。和国安不一样，他们看来知道李的一切情况。他们知道他计划建立一个党，她帮他修改过的那份党纲，甚至知道有关炸弹恐吓的那次谈话，刘回忆道。但奇怪的是，他们把所有李的想法都归罪于姜，那个热力公司干部。<br />
&nbsp; &nbsp; 在开头的几个星期里，警察审讯了刘七八次，他们还暗示李、吴和姜也都被捕了。<br />
&nbsp; &nbsp; 在一次审问中，刘不得不解释，她写的关于“柿油党”的文章只是开玩笑，并没有这样一个组[和]织存在。但警察并没有在她的文章上浪费时间，一个预审员甚至告诉她，她的文章没有问题，她回忆道。<br />
&nbsp; &nbsp; 相反，他们逼她作证指控姜。刘只见过他一次。但迫于恐惧和压力，当警察把姜描述成一个希望用暴力推翻政府的激进分子时，她同意了。“警察写了下来，我签了字，”刘说，她的声音越来越小。“我不敢不签。”<br />
&nbsp; &nbsp; 几个月过去了，刘与另外三个妇女一起坐在一间小牢房里，其中一个是已被定罪的杀人犯，刘努力理解自己的处境。她的家人不允许探视，律师告诉她，如果颠覆罪名成立，她将面临10年徒刑。大学和互联网显得很遥远。<br />
&nbsp; &nbsp; 但监狱之外，她被捕的消息传开了。她的网友发起了请愿活动，征集到了数千个签名。一些网民在自己的网名前面加上“不锈钢”来表示抗议。人[和]权组[和]织和外国政府为她的获释而游说。<br />
&nbsp; &nbsp; 2003年11月28日，温**总理访问美国之前几天，在被拘禁超过一年之后，政府释放了刘和吴。同一天，法庭判决38岁的姜颠覆罪名成立，判处有期徒刑四年。<br />
&nbsp; &nbsp; 宣布这个消息的时候，香港一个人[和]权组[和]织说，李的一个朋友与他们联系过，并告诉他们李也被释放了。但刘从负责她的案子的检察官那里听到了不同的说法。他告诉刘，李没有被释放，他被指控了很多罪名，不可能释放他。<br />
<br />
在幻影世界中寻找一个幽灵<br />
<br />
&nbsp; &nbsp; 刘被自相矛盾的说法弄糊涂了。一天晚上吃饭的时候，她父亲提出一种解释：李可能是公安的线人。<br />
&nbsp; &nbsp; 他指出在法庭文件中，检察官把吴和姜作为她的同案犯，但把李另案处理。他也注意到每个被告都有家人出来为他们的释放而呼吁--除了李。<br />
&nbsp; &nbsp; 刘平静地接受了她的朋友可能是公安的特务的说法。但她的脑子在飞快地思考这种可能性：这就是为什么他想让自己给他介绍其他网友吗？这就是为什么他坚持要她修改党纲吗？他也陷害了姜吗？<br />
&nbsp; &nbsp; 刘研究了姜的判决书--他为自己作了无罪辩护--并且注意到了一些奇怪的东西。<br />
&nbsp; &nbsp; 物证清单中列出了她和李在他办公室中的照片，还有一张她、吴和李在李的住处的照片。但她说，他们没有拍过什么照片。一定是有人看着他们，并且偷拍的。李知道这个吗？<br />
&nbsp; &nbsp; 这个发现让人心寒，而刘担心如果她继续发问的话，当局会做些什么。但她也很愤怒。“如果这些都是他捏造出来的，他就把我们都陷害了，”她说。她也为自己与警察合作，协助他们指控了姜而感到内疚。“我让他失望了，”她说。“我至少应该找到李，查出这到底是怎么回事。”<br />
&nbsp; &nbsp; 于是不锈钢老鼠开始挖掘了。她在互联网上给李发了信息，但没有回复。因此她在网上说出了自己的怀疑，并寻求帮助。<br />
&nbsp; &nbsp; 有时这就象是在寻找一个幽灵一样。一个在警察部门工作的朋友查了城市户籍记录，却发现北京户籍中没有李的名字。有人查找了李过去在网上发过的帖子来寻找线索，却发现他总是转贴别人的文章，从不自己写文章。<br />
&nbsp; &nbsp; 还有一条可望而不可即的线索。一个名叫西风古道--引自中国古诗--的网友曾经帖过两篇文章要求政府释放李。后来他还给一家香港刊物写信，说李于11月底获释，并和自己见了面。<br />
&nbsp; &nbsp; 信的作者自称是李的朋友，刘在狱中时，刘的父亲还与他通过电子邮件。现在刘试图与他联系。但他没有回复，刘怀疑西风古道可能就是李本人。<br />
&nbsp; &nbsp; 后来，五月初的一天晚上，刘的手机响了。是李。她很吃惊，问李到底是怎么回事。他回答说自己于一月份被释放，现在正在北京找工作。他还说自己被牵扯到两个案子中去，他打算把事情经过帖到网上。<br />
&nbsp; &nbsp; 他建议我们见面谈，刘回忆道。她同意了，李让她以后给他手机发短信。<br />
&nbsp; &nbsp; 但就像他的突然出现一样，他又突然消失了。刘发的短信没有回复。她不停地给他打电话，但线路似乎一直不通。李也没有履行承诺帖出事情经过。<br />
&nbsp; &nbsp; 刘试图去找李的办公室，只发现那座楼已经被拆除了。她找到李的名片上的公司。公司经理否认李是他们的雇员，说他只是在公司租了间办公室。<br />
&nbsp; &nbsp; 最后，刘鼓起勇气回到李的住处。她在一份司法文件上找到了他的地址。他家所在的楼是北京市监狱系统的职工宿舍。<br />
&nbsp; &nbsp; 敲门没人回答，她试图找他的邻居帮忙。没人知道李的名字或长相。<br />
&nbsp; &nbsp; 但刘留了一张字条，几天之后房主打来了电话。他也不知道李的名字。得知这个地址出现在司法文件上，他说这一定是搞错了。他家2001年搬到这里。2003年五月开始把这套房子租出去，但从来没有租给过符合李的相貌的人。无论如何，刘到李家来是在2002年9月。<br />
