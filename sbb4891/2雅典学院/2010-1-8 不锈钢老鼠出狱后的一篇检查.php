													<h2>还真让我找到了</h2>
						
						
																											<div id="postmessage_420693" class="t_msgfont">老戚：刘荻为何变得如此诡异？<br />
(首发稿)<br />
<br />
<br />
文章摘要: 此女子那一段坐牢的日子甚为可疑。现在从她后来的言行可看出她可能已成为党的孩子。她是肩负重要使命走出监狱的，并不是外界的呼吁使她出来的。而是她答应为党工作，然后老大哥放她出来的。 <br />
<br />
<br />
作者 : 老戚, <br />
<br />
<br />
l表rg：9/25/2006<br />
<br />
笔者用这个题目还是较客气的了。 <br />
<br />
今天是九月二十三日。高智晟已失去自由三十八天了，莫少平律师仍然未能和他会面。高智晟先生的命运牵动了不少人的猜想，笔者倾向于高智晟先生可能被重判的看法。不是老共有多么强大而是国人的懦弱所致，枪打出头鸟。高智晟先生、郭飞熊先生之后大陆再无硬汉了。 <br />
<br />
笔者曾经定下的四个题目： <br />
<br />
《刘荻为何变得如此堕落？》 <br />
<br />
《 刘荻的剧变看黄种人的劣根性》 <br />
<br />
《 刘荻的快速沉沦看中国年轻人的颓废》 <br />
<br />
《 刘荻为何变得如此诡异？》 <br />
<br />
最后还是选了个比较客气的题目。 <br />
<br />
笔者和刘荻还是有不少缘份的，即使现在她仍然是我的邻居，在《自由中国论坛》的专栏作家里老枭排第一，她排第二，我排第三。既然是邻居本应友好相处。或者井水不犯河水。但此女子的表现，特别是高智晟先生出事后，此女子的表现更是令人齿冷。 <br />
<br />
几年前我学会上网时，凑巧遇到她出事，当时的不锈钢老鼠掀起的网络维权风暴令我终生难忘。2000多海内外良知人士、人权勇士的签名，大量作家为这奇女子撰文。我当时刚好撰写一些系列小文，向世人介绍挑战强权、结束帝制的杰出人物。于是我便有感而发，撰写了《当代英雄――才女刘荻》。 <br />
<br />
千人为雄，万人为杰。今天中国正处于剧变的大时代。大时代是英雄辈出和魔鬼辈出的时候。由此可以死也由此可以生的时代。 <br />
<br />
而我选择的英雄标准很简单：生活在真实中，用知识学问才华良知勇气结束中国千年帝制，建立一个在这块土地从未有过的真正主权在民的宪政人权自由民主法治的国家的人就是英雄。 <br />
<br />
当时选了余杰、老威、刘晓波、李慎之、徐勤先、王友才、东海一枭、任不寐、王维林、郑恩宠、高勤荣、杜导斌、李健强、张思之、杨银波、师涛、卢跃刚、蒋彦永、王克勤、胡适根、刘水、张林、郑贻春、刘飞跃、吕邦列、郭飞熊、新青年四君子杨子立、徐伟、靳开科、张宏海、黄万里、赵达功、赵岩、白振侠、郭光允、黄金高，赵昕、湖南衡阳三君子余志坚、喻冬岳、鲁德成、清水君。还有四女子：丁子霖、高耀洁、王友琴、刘荻。 <br />
<br />
选择的标准主要是国内。而且唯一的主题是否有利于结束中国五千年的黑暗帝制。 <br />
<br />
但还是写得较简单。 <br />
<br />
有两个原因令我暂时停止了这个系列的写作：一是电脑知识太差，电脑也太残，经常无法上动态网；二是出了高智晟/焦国标、袁红冰、杨天水/郭国汀、黄琦、严正学等一批更具震撼力的人物，我很难落笔。 <br />
<br />
现在由于能较正常上动态网，加上袁红冰先生《自由圣火》愿意刊用，我又开始了这个系列的写作。只要我不出事，我一定会写到中国成功转型为止。也感谢袁红冰先生的赏识。 <br />
<br />
北师大能出刘晓波、杨天水、艾晓明、刘荻也算是不错的大学了。钦州人能考上北师大看来难度挺大，（就算是考得非常好，分数比北京的学生高得多也不一定能上此大学，原因是外地人）这是中国教育部的歧视造成的。 <br />
<br />
反特权就是反对一切形式的特权。这种歧视性的招生便是中国教育部黑幕之一。所以北京人的刘荻考上北师大没啥值得骄傲的。她值得骄傲的是她因在网上发言被党和政府抓去关了的那一段时间。 <br />
<br />
所以老枭《恭喜刘荻、贺喜刘荻》思路甚对。如果不被抓，恒河沙数的大学生有谁注意？中国的大学我都不读。因为天才永远都是天才！天才是天生的。象老枭、杨银波、焦国标、高智晟这类天才从未进入过高等学府，有谁能否认他们不是天才吗？ <br />
<br />
天才从不受人影响；天才总是象一阵风呼啸而过；天才都是划时代的人物；天才就象划破黑暗天空的一道闪电！ <br />
<br />
但是此女子那一段坐牢的日子甚为可疑。现在从她后来的言行可看出她可能已成为党的孩子。她是肩负重要使命走出监狱的，并不是外界的呼吁使她出来的。而是她答应为党工作，然后老大哥放她出来的。 <br />
<br />
看她两篇文章便发现她多么诡异：《炮打民主，我的一张大字报》。她为何如此刻骨仇恨在中国建立民主？ <br />
<br />
《为什么我不参加绝食？》 <br />
<br />
当高智晟律师发起并实践维权接力时，她又冒天下之大不韪和全人类的良心作对。 <br />
<br />
除了捣乱，她出来后何尝做过一件好事？许多因参与签名救她的朋友也被她冷嘲热讽，把签名一件好事破坏得一塌糊涂。几乎所有为她签名和呼吁的人都被她无情嘲弄。 <br />
<br />
而由于她是《自由中国论坛》的付站长和超版。在这个论坛凡是正直的良知人士均遭其百般嘲弄。其中受害者没有一千也有几百。大多数被她或她的同伴气走或赶走。 <br />
<br />
笔者由于不介入争论和讨论，是因为不想受气或被气死。 <br />
<br />
但笔者也被其奚落不少。好像我和她有十冤九仇似的。 <br />
<br />
比如我撰写的《黑暗的中国》遭到刘晓波先生批评时，我因对刘晓波先生的景仰便写了一首诗歌感激他。刘荻小姐却说我&quot;肉麻&quot;。 <br />
<br />
我提议蒋彦永、赵紫阳、丁子霖、高智晟角逐诺贝尔和平奖。她说我不够格。我提议推荐而已。 <br />
<br />
说我志大才疏。总之不一而足。问她能否成为昂山素姬？她浑不当一回事。 <br />
<br />
总之谁听得见她一句好话？ <br />
<br />
比我更惨的是另两位专栏作家：张国堂先生和山东鱼台老翟。张国堂被气得说要抓她坐牢！而老翟的文章都被她驳得一无是处。 <br />
<br />
如果在现实生活有这样尖酸刻薄的小女子，我虽然信奉&quot;好男不跟女斗&quot;。但也绝不会忍辱负重。 <br />
<br />
而对遭受迫害的F功、对&quot;九评&quot;、对袁红冰先生等更是怀着不共戴天的仇恨。 <br />
<br />
更为恶劣的是在高智晟先生出事后，她直说高智晟先生有精神病，只要肯承认有病，当局便放他出来。和高智晟先生一般出色的被逼走至加拿大的郭国汀更是被她运用其狗屁不通的心理学百般嘲弄。 <br />
<br />
总而言之。 <br />
<br />
此女子的堕落是铁定的。 <br />
<br />
老枭说她还是好人，只是嘴巴刁而已。这是老枭宅心仁厚。 <br />
<br />
我是不惮以最坏的恶意猜测刘荻的。她所谓受监控，无非又是一种保护性措施。 <br />
<br />
我还是心存一丝幻想，就象余杰、王怡拒郭飞熊见布什是受协迫的。而刘荻的堕落亦是受协迫的。 <br />
<br />
善良的人们，要警惕那些佛口蛇心的家伙啊！</div>

							
							
							
							
