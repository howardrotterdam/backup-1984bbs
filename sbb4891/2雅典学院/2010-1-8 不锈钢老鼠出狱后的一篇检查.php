													<h2>不锈钢老鼠出狱后的一篇检查</h2>
						
						
																											<div id="postmessage_420181" class="t_msgfont">--------------------------------------------------------------------------------<br />
<br />
我于2002年11月7日被北京市公安局刑事拘留，又在被关押1年另21天后被取保候审，至今已经过去将近3个月了。其间于2003年12月25日被北京市检察院第二分院以“犯罪情节轻微”为由不起诉，解除取保候审。在这段时间里我仔细考虑了我的行为，现在做出交代和检查。<br />
<br />
我们的案件总共有四个人，我，ID叫不 锈 钢 老 鼠，另有三个同案，分别是姜力钧，吴一然和李毅兵。我以前一直觉得李毅兵这个人不太聪明。我们是在互联网上认识的，我们常常在聊天室里，在QQ上聊天，聊些政治，聊些国家大事。我们喜欢在网上对拥护党和政府的人士冷嘲热讽，甚至于对爱国者施以语言暴力：猛拍板砖，一时间视野之内血肉横飞。我在网上还发了很多火药味十足的文章，我号召网友们集体投诚，向党和政府示威；我恶毒地讽刺过国家的安全机关；我还建议过大家用贪污腐败的办法来把党搞垮。我们也谈到过如何采取措施预防“网警”，如交换些设置代理服务器的方法之类。有时我们也走到网下，到麦当劳一类的地方去坐坐聊天，也谈过一些政治、历史、哲学等话题。我从小很喜欢读书，也读过很多书，如索尔仁尼琴的《古拉格群岛》，但我更喜欢的还是小说：乔治．奥威尔的《1984》和安东尼．伯吉斯的《发条橙》。读《发条橙》的时候我曾经感触颇多：“上帝要的是善呢？还是要向善的选择？”“心理学能够用来作为控制人类，剥夺人类自由意志的工具么？”为此我还写了《有自由，才有美德》一文贴在网上。我自认为是个自由主义者，当时我认为，自由，包括宪法里写的言论、结社自由，是当真的。<br />
<br />
李毅兵和我说过，他认为关于中国的自由民主等问题，一切能说的都已经说尽了，剩下的就是要行动了。我没当真，认为他是头脑发热。有一天，他说有个东西要给我看。神秘兮兮地拿出来后，我看到的是一份“自由民主党”的“党纲”。大致看了一下，说实话，写得不怎么样，尤其是文中有“暴力推翻政府”。我反对，我说。于是他要我帮他改改。我开始拒绝，但是禁不住他反复要求，我勉为其难地答应了。我把“暴力”的提法删除，还对其他几处文字做了修改。但是我心里依然不同意他的想法，现在想起来，这件事除了把人往监狱里送以外，没有任何意义。所以后来我后悔了，我极力劝阻他不要当真成立什么政党，还多次要求收回我修改过的“党纲”。因此所谓“自由民主党”，自始至终都只是个停留在纸面上的东西，也就是说除了这一纸“党纲”以外，一无所有。<br />
<br />
2002年9月的时候，系里的乔老师找我谈过一次话，意思是有关部门点我的名了，让我注意点自己的言行。我答应了，从那以后我几乎没有在网上发帖子。但是我也没有老实交代李毅兵和“自由民主党”的“党纲”。部分是因为我觉得他已经听从我的劝告，这时再把他交代出去，不太厚道，也不太够哥们。<br />
<br />
同年11月6日晚，一些可能是警察的人（因为他们穿的是便衣）来找我了解情况。他们主要想了解李毅兵的情况，我也出于同样的原因没有说。后来他们要我带他们去找据说是李的一处住宅，或者要我打电话把他约出来，我都没有答应。当时我认为，我那么做是对得起朋友的。<br />
<br />
第二天，也就是2002年11月7日，北京市公安局以“涉嫌违反国家法律”为由对我刑事拘留。随后我被押上一辆汽车，夹在两个警察中间，送到北京市看守所。我听车里的警察说，同时被抓的还有两个人，我认为应该是李毅兵和吴一然。<br />
<br />
他们把我带到看守所后，一两个小时后便开始审问我。在随后的一年多时间里，公安局共提审过我十几次。有一点我应该说明的是，在这一年多时间里，无论是公安局还是看守所的警察，都从来没有打过我或者虐待过我。这一点与我以前读到的和想象的不同。当公安局的预审员问我，李毅兵和我做了什么的时候，我一开始还是说不知道。但是当他们说到李毅兵的说法和我不一样时，我的心往下一沉，我认为李“出卖”了我，于是我就把我所知道的情况都向预审员交代了。随后我说自己是年轻幼稚被人利用，请求政府再给我一次机会，对我从宽处理。<br />
<br />
在看守所里，公安局和看守所的警察对我都还是不错的。也许是看我年轻又是大学生，所以他们常常找我谈话，尽力教育挽救我。为了让我好好学习，公安局的警官后来还让我家人给我拿了一些法律书籍和课本、小说、杂志等送去。在看守所里读到了自己心爱的书，我焦虑的心情舒畅了不少。在这一年多里，我不仅学习了很多法律知识，而且更加深入地了解了中国社会的状况，这些都是我的宝贵收获。<br />
<br />
在看守所里我也听到过外边声援、呼吁的声音。说实话我很感激他们。在看守所里的时候知道大家还没有忘记你，心里确实是热乎乎的。但是我也很惶恐，因为本质上我是个害羞的人，绝对不是一个能够做领袖，能够在众人面前发表演讲或者高呼口号的人。老鼠总是喜欢躲在暗处的。我想大家恐怕把我抬得太高，使我今后很难做我自己，或者过自己喜欢的生活了。我也绝对不是大家想象中的英雄，在看守所里，很多时候，我只是一个无助的，给吓坏了的孩子。<br />
<br />
预审员告诉我这些的时候说，以前的事是有人在利用我，现在还是有些别有用心的人想利用我。然后他们要我写悔过书，我写了。是的，没有人强迫我，我是自愿的；我是有自由意志，有自由选择的权利的！但是我摆脱不了那种成为发条橙的感觉。小亚历克斯接受路氏技术的治疗，被剥夺了自由选择的权利，只能做为一架行善的小机器被释放后，所有的人都打他，虐待他；所有的政客，内务部长和反对党领袖都企图利用他作为政治工具。我不知道自己为什么想到这个，也许是因为那种无时无刻不存在的无助感。我感觉自己无能为力，不由自主。我觉得我不具有那种强大的人格力量，很多时候我甚至控制不了自己的情绪，也摆脱不了那种斯德哥尔摩综合症似的感觉。我觉得我已经变了个人似的，但是我的理智为此感到悲哀。这件事让我深刻认识到了自己内心的软弱和渺小。<br />
<br />
我被释放以后，回到了温馨舒适的家里，家人都非常高兴，都对我很好。当然朋友们也都很高兴，走到哪里大家都热烈欢迎我，我并未受到像小亚历克斯那样的对待。后来我听说姜力钧被判刑四年，而关于李毅兵的消息却有点变化：一开始我们听说他与我和吴一然一同被释放，但是却没有他的消息；最近又听说是发生了错误，他并未获释，而是因为另有案情，被另案处理，但是这件事我以前也一点都不知道。<br />
<br />
我似乎又回到了原来的生活中：又开始与以前的朋友，还有些新朋友见面；又开始上网、读书，希望能够返回学校继续上学；不同的是现在有些记者来采访我。我似乎又抛弃掉了那个在看守所里软弱无力的我，变成一只大出风头的老鼠了。可是我又遇上发生在小亚历克斯身上的事了：在美国版中被删掉的《发条橙》的最后一章里，小亚历克斯遇上了当年帮派里的伙伴彼得，彼得已经结婚了。小亚历克斯忽然认识到暴力只是属于青春的，而青春必须逝去。小亚历克斯长大了，想要结婚生子，过正常人的平静生活，并且认识到自己只是上帝手中的一枚发条橙。是呀，我也遇到了这样的事：出来以后，我发现好几位老朋友要么是已经结婚了，要么是正在准备结婚；也有几个朋友，包括我又见到的，我的同案吴一然，现在准备出国或者移民。我也想起似乎是一个人格测验中的一个判断题：“一个人年轻的时候有些反叛的想法是正常的，但是当他们长大以后就应该把这些想法丢掉并且安定下来。”这个说法是对的吗？我不知道，也许是因为我还没有长大，但是我也有些想法：也许小MM老鼠该去找一个GG鼠了。<br />
<br />
现在我也有时间反思一下自己过去的所作所为，以及我自己在这件事中应该吸取什么样的教训。我认为在整个事件中，我所犯的最大的错误，和人类所能够犯的最大的错误一样，都是愚蠢。我是相信个人主义的价值观，相信“动机与目的统一”，也就是相信“自私”的。人总要为个人利益最大化而努力。当然个人利益不一定指钱，也可以包括思想的利益等等。但是我却为了一个自己并不同意的“党纲”被关了一年，这有违我自己的原则。我无法解释自己当初究竟为什么要这么做，只能用愚蠢来形容自己。为了一件不值得的事，为了一个不值得的人，我只能说自己愚蠢。这一年我究竟得到了些什么？网络上的“名声”吗？真正让我苦恼的是，大家心目中的老鼠，并不是真正的老鼠，如今“不锈钢老鼠”已经很大程度上变成了一个符号，而不再属于我了。按照拉康的观点，任何一种语言符号本身就是对潜意识的压迫和伤害。如今“不锈钢老鼠”这个符号已经对真正的“老鼠”--我的无法用语言来形容的内心构成了伤害。现在我觉得自己已经不是以前那只充满主体性，在宇宙中到处抢银行的“不锈钢老鼠”，而成为了完全丧失主体性的“发条橙”。这些都不是我真正想要的，我可以说是被迫接受这些的，虽然我得承认，接受采访不像我当初想的那么可怕。“不要被别人利用”这句话说起来容易，做起来难，因为也许人和人之间的关系，就是互相利用，各得所需。可我真正想要的是什么，我自己也说不清楚。以前写过一个“我的价值观”，理想和亲友是我最先抛弃的，生命是短暂的，而自由也只不过是个幻影，剩下的只有创造力，我真正希望过的生活就是有创造力的生活。我希望在创造中，还能找回原来的老鼠，但是这并不容易。因此也许我应该在知识中去寻找我的未来了。顺便说一句：经过了这件事，亲人和朋友，在我心中占据了不可替代的位置。<br />
<br />
请相信，这篇检查中，我说的每一句话都是真心的。请原谅我在文中使用了一些也许让人难以接受的修辞和文学手法。请理解：这件事对我的伤害，我至今还不能完全面对。这些文学手法也许是为了避免对我自己更大的伤害而采取的一些防御措施。我需要时间去消化这些痛苦，好让我有一天能够真正鼓起勇气来面对这一切。</div>

							
							
							
							
