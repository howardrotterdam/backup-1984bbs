													<h2>真实的张学良</h2>
						
						
																											<div id="postmessage_375496" class="t_msgfont">谈中国近代史，离不开国共两党；而研究国共两党史，离不开西安事变，因它是两党关系的转折点；但西安事变的主角张学良，活到101岁，就是不肯说出事变内幕，直到2001年10月14日去世，把他知道的秘密带进了坟墓。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 自1936年西安事变至今几十年中，海内外出版了很多关于这一事件的书籍，截止1991年底，仅中国大陆就已出版了23种；另外还有张学良接受各种媒体采访的谈话。但海峡两岸对张学良的评价却像黑、白那样两极：大陆把他誉为“民族英雄”，台湾定他为“历史罪人”（解除党禁报禁后，台湾已有不同声音）。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 到底怎样给张学良盖棺论定？要回答它，就必须弄清楚七个关键性问题：第一，日本侵占东三省，张学良为什么不抵抗？第二，蒋介石为何不同意“抗日 ”？第三，张学良为什么发动兵谏？第四，西安事变造成什么后果？第五，谁应该对西安事变负责？第六，张学良为何近一生被囚禁？第七，张学良怎样看待中国那段历史？ <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; ●第一，张学良为什么在东北不抵抗？ <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 1991年5月，在张学良被软禁50多年后首次获准来美国那次，在东北同乡会的安排下，我和李勇、徐松林、刘宾雁等八个东北人在纽约以老乡的名义去看望了他。在长达150多分钟的聚谈中，张学良有意无意地解释了很多问题。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 对于日本入侵东三省，东北军不予抵抗，世间广泛流传是因为蒋介石曾有一手谕给张学良，不让其抵抗。我问张学良“到底有没有这个手谕呢？”张学良明确回答说，“是我们东北军自己选择不抵抗的，我当时判断日本人不可能要占领全中国，我想尽量避免刺激日本人，不给他们借口扩大战事。我们采取‘打不还手，骂不还口’，但最后不能‘杀不出血’。杀到全中国来了，我们只能拼死一搏。” <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 1990年12月9日，张学良在台湾软禁几十年后首次接受记者采访（日本NHK电视）时也是这样解释：“当时的中央不是蒋总统，是孙科（蒋那段时间下野，国民政府主席是林森，行政院长是孙科），当时中央给我的指示是，相应处理。不抵抗的命令是我下的，我承认那时判断错误。我下令不抵抗是为了大事化小，小事化了。” <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 除了所谓“错估局势”，张学良可能还有不愿直说的原因，那就是东北军根本不是日本关东军的对手，如果抵抗就是全军覆没。包括张学良在内的中国军阀们，都是靠部队多少而存在的，没有了东北军，就没有了张学良的地位。东北深山老林多，盛产土匪。张作霖的东北军其实就是最大宗的土匪，在小股土匪眼里它是 “正规军”，但碰上装备精良受过训练的日本关东军，东北军就露出土匪本色。张学良曾为争夺铁路权率东北军和苏俄军队交过手，结果大败，被迫签订屈辱条约。那次惨败，等于向日本展示了东北军不是东北虎，而是纸老虎。1933年3月热河一战，日军仅用128名先头骑兵，几乎兵不血刃就攻占了热河省会承德，可想而知东北军的战力和张学良的指挥能力。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 汪精卫虽然后来成为汉奸，但在918事变时，他是主张抗日的，他继孙科后出任行政院长，曾多次给张学良发电报，催促他出兵抵抗，责备张学良“去岁放弃沈阳，再失锦州，致三千万人民，数十万土地，陷于敌手……”但张学良总是推诿说粮饷不足，拒不行动，并还暗指汪精卫无权指挥东北军，结果气得汪精卫辞职出洋，认为这个行政院长没法干了。 <br />
&nbsp; &nbsp; 　 <br />
&nbsp; &nbsp; 在日本关东军攻打热河之前，中国有识之士都认为张学良这个当时吸毒成性、沈缅女色的公子哥根本没有能力指挥整个华北的抗日，军政老将宋哲元、商震、庞炳勋等都要求蒋介石亲自北上，指挥抗日。著名知识分子胡适、丁文江、翁文灏三人当时曾联名致电蒋介石，指出华北抗日，不是张学良能够领导的，热河如果再轻易失守，政府责任难逃，吁请蒋介石速来华北亲自指挥。 <br />
&nbsp; &nbsp; 　 <br />
&nbsp; &nbsp; 蒋介石接到胡适等人电报后覆电说，他第二天就北上。然而当天夜里，热河全境就被日军占领。蒋介石后来感叹说：“我估计日本要用六个师团的人，日本国内和台湾（当时在日本手里）都得动员兵力。我们每日都有情报，知道日本没有动员，故料日本不过虚声吓人而已。不料日本对汤玉麟、张学良的军队（能力的了解）比我们知道得多。” <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 当时进攻东北的日本军队只有几万人，而仅张学良的东北军就有30万部队，但热河就这样轻易失守，对此胡适非常悲愤，在《独立评论》上发表了“全国震惊之后”一文，认为热河失守，张学良罪责难逃。胡适毫不客气地指出：“张学良的体力与精神，知识与训练，都不是能够担当这种重大而又危急的局面的。” <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 地质学家丁文江也在同期《独立评论》发表了“给张学良的公开信”，批评张学良既无指挥能力，又不亲赴前线督师，但却恋权不放，陷东北百姓于水火，贻误国家。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 虽然张学良在纽约聚谈时澄清，当时没有蒋介石“不予抵抗”的一纸手谕，但从整个抗战背景来看，蒋介石对张学良在东北不抵抗基本是持某种默许态度的，主要原因是蒋对当时中国和日本的军事实力悬殊有比较清醒的认知，倾向于忍让、不扩大事端，以争取时间。从这个意义上说，张学良采取不抵抗，在策略上也不完全是错，它可以为国民政府保存一定军事实力，以后从长计议。因此可以说，东北不抵抗是张、蒋两人的共识。史学家争论到底是张不抵抗，还是蒋下手谕，并无实质意义。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; ●第二，蒋介石为何不同意“抗日”？ <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 东北失守之后，蒋介石为什么不同意张学良要抗日的主张，最后激发出西安事变？从常识和逻辑来说，当时连一般老百姓都无法容忍日本侵略，作为一个具有强烈民族主义情绪的中国领导人，蒋介石怎么可能就不准备抗日呢？从蒋介石的一生可以看出，尽管他和毛泽东有很多不同，但有一点非常相似，他们都是强烈的民族主义份子，这点从后来他和毛泽东都无论如何要坚持“一个中国”可以看出。蒋介石宁肯中华民国忍辱退出联合国，也绝不放弃大陆版图，更不走台独。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 而且八年抗战的历史事实已证明，蒋介石不仅坚定抗日，而且领导了整个抗战（国民党部队承担了主要战场，共产党军队仅是敌后打打游击）。据香港学者李谷城的研究统计，八年抗战，国民党军队伤亡340万人，共产党军队伤亡61万人。从牺牲的人数大小也可以看出谁在承担主要的战场和责任。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 从台海两岸出版的史料都可以证实，蒋介石并不是不抗日，而是在抗日的时机上和张学良有严重分歧，蒋介石主张“攘外必先安内”，即抗日之前，先统一内部，清剿红军。这种政策主要出于两个考虑： <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 一是当时中国虽结束了军阀割据，但政令难以统一，尤其是共产党不仅仍拥有军队，并割据陕北一方。蒋介石和张学良的最大不同之处是，蒋对共产党的本性有相当的了解，深知如果不铲除红军，它会利用抗日之机得以发展而后患无穷（后来的历史正是这样演变的！）。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 蒋介石苦心经营的五次围剿红军和共产党，虽然前四次失败，但第五次获得成功，红军被迫进行所谓“两万五千里长征”的大溃退。溃退到陕北的红军仅剩下几万人，蒋介石多年的艰苦剿共，终于有了眉目，他怎么让它功亏一篑呢。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 二是以当时中国的国力无法与日本匹敌，必须尽量争取时间，以储备力量。史学家黄仁宇在《从大历史读“蒋介石日记”》一书中说，当时蒋介石“深知两方的实力，如即此仓卒应战，只有自取败亡。”因此蒋的策略是，尽量避免全面抗日的爆发，以争取时间组建更多师团，建造兵工企业，构筑能够对日作战的国家能力。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 1934年，蒋介石就把为抗日而建立的国防设计委员会改组为“资源委员会”，直属蒋直接领导的“军事委员会”，在湖南、四川、湖北等大后方创建军工厂和重工业。西安事变时落到张学良手里的蒋介石日记中对这些国防设计有详细描述（这也是促成张学良醒悟的原因之一）。在西安事变前一年，蒋介石还坚持“ 和平未到完全绝望之时期，决不放弃和平；牺牲未到最后关头，亦决不轻言牺牲。” <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 但蒋介石为什么不把他的想法告诉群情激昂要求抗日的中国民众呢？“918”之后，日本内部对是否攻占整个中国也有分歧，当时日本政府向“国联 ”（国际联盟）提出的对华五项条款，其中主要有“有效保护日本臣民在满洲之一切和平业务，尊重日本在满洲之条约利益”，日本“尊重中国土地之保全”。虽然这种条款是完全不合理的，但中国仍有可能和日本周旋，拖延日本全面入侵中国的时间，以积蓄自己的力量，从长计议。因此如果作为当时中国领袖的蒋介石公开这种战略部署，等于告诉日本人，中国正卧薪尝胆，准备全面抗日，会刺激日本鹰派提前全面侵华。因此国人当时多不知蒋介石的这种国防设计。日本占领东三省后，中国人慷慨激昂，誓言抗日，尤其是大学生和小知识分子们，更是游行示威。在高喊“刀枪不入”的义和团文化背景下，很容易热血沸腾，难以听进去理性的声音。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 当时中国知识界对此保持清醒头脑的是胡适和他的弟子傅斯年等人，胡适发表“华北保存的重要”一文，主张“停战谋和”，指出以中国之国情“不足以对日言战”；“战则必大败，而和则未必大乱”。胡适后来还提出，“日本倘能自华撤兵停战，中国不惜承认满洲国”。胡适为文解释说，当年比利时曾被德国占领，法国割两省给普鲁士，都是由于实力悬殊而采取的权宜之计，后来有实力又收回。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 但胡适的见解不仅无法成为主流声音，而且被指责为“汉奸”“卖国贼”。胡适当年曾感慨：中国有古训“威武不能屈，富贵不能淫，贫贱不能移”，应该再加一条“时髦不能跟”。胡适和汪精卫的本质不同是，一旦所有谋和手段失败，日本进攻全中国，胡适则坚决主张全民血战，直至最后一寸土，最后一个人。这也是后来他为什么打破不做官的自律，在蒋介石的几次恳请后同意到美国做“中国大使”争取美国援助的原因之一。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; ●第三，张学良为什么发动兵谏？ <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 热河失守后，全国舆论沸腾，责骂政府无能、不抗日。据大陆作家王朝柱的《张学良和蒋介石》一书（台湾国际村文库书店93年出版）中引用的张学良秘书王卓然当年的日记，热河失守后，蒋介石曾和张学良密谈，蒋说，现在全国群情沸腾，我们两人（指蒋张）同乘一条小船，现在风浪太大，必须下去一人，否则船沈；将来局势平稳，下船的人再上来。张学良表示：“我身体不好，精神萎靡，东北失守……这次热河失守，我更是责无旁贷。愿引咎辞职。”随后张学良下野，去欧洲“考察”。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 从此张学良被人骂为“不抵抗将军”，连他的弟弟都因此被人羞辱。张学良的四弟张学思曾任解放军海军参谋长，文革时被红卫兵批斗打死。据1996年 6月1日上海《文汇读书周刊》发表的刘永路整理张学思档案发现的材料，918后张学思的内衣曾被同学用墨汁涂写“不抵抗将军之弟”，张学思感到羞辱难忍，闯进张学良所在的北平顺承王府，拿着那件内衣，面责大哥为什么不抗日。本来是张、蒋的共识――为避免扩大事端而不抵抗，最后责任落在张学良一个人身上。张学良的痛苦、压力可想而知。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 但在这种情况下，从欧洲考察回来后，张学良不仅没有被派去抗日，反而被委任为西北剿共副总司令，负责攻打红军。张学良本想通过抗日洗刷“不抵抗将军”的污名，现在不仅无法实现，而且东北军和红军交手两次，两次大败，张的两个师被歼灭。蒋介石不但不给兵员补充，还把两个师的建制取消了。这使张学良疑心，蒋可能在一箭双雕：剿匪成功，红军被消灭；不成功的话，东北军被削弱，而蒋的中央军则保全实力，因此更对“攘外必先安内”政策抵触。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 张学良是在父亲被暗杀后匆促继承父业，成为东北军最高长官。他从小不爱读书，喜欢的是毒品、女人，他的知识水平，可能都不如那些上街游行空喊抗日的学生，更谈不上对中国当时的国情认知到胡适那种水平；而且他对蒋介石的战略部署根本不知情（后来看到蒋的日记醒悟，说明原来他并不了解蒋不仅要抗日，有周密国防计划，而且还要重用他）。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 除此之外，张学良还有一个隐秘的动机，那就是利用兵谏逼蒋抗日之后，他就可以在西北联合红军和西北军而割据，不仅洗刷不抗日的污名，而且可与蒋分庭抗礼。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 事变之后张向蒋提出的条件，不仅有停止剿共立即抗日，还有成立西北联军和西北抗日联合政府等政治要求。张学良所以提出这样的条款，是因为他在共产党人周恩来等秘密接触谈判中，周已表示要在西北建立以张学良的东北军为主、西北军和红军为辅的“三位一体”的西北联军，成立和南京中央政府分庭抗礼的“西北抗日联合政府”。据大陆新公布的史料，张学良在首次和周恩来密谈时，就给了“见面礼”两万大洋和20万法币，完全是按照土匪之间的规矩做的，显示张学良对共产党的性质根本缺乏了解，他把红军当成西北军一样的地方武装；而周恩来对张学良彬彬有礼，一口一个“张将军”地推崇，也使张学良产生红军和西北军都要推举他为“西北王”的幻想。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 张学良当时已经看到回东北无望，因此联合红军和西北军建成“西北联军”，并成立以他为首的“西北抗日联合政府”，就可以得到苏联和斯大林的军事援助，而在西北割据和称王。当时东北军有近20万部队，西北军3万人，再加上经过长征剩下的2万红军，这支25万人的“西北联军”是蒋介石难以对付的。因此纽约州罗彻斯特理工大学中国历史教授朱永德在提交给西安的“西安事变60周年研讨会”论文中就置疑说，“张学良事先真的仍准备一旦蒋答应终止内战一同抗日就会将蒋释放？从今日的资料来看，当时的计划是在组织西北联军，发动西北大联合，筹划西北抗日联合政府。” <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 但张学良即使有这样的认知局限和隐秘动机，如果没有另两个条件，西安事变也不会发生：一个是他权倾一时。张学良从他的军阀父亲张作霖的所谓军事学校“讲武堂”一毕业，就被任命为旅长，那年他才19岁；而当年底就被晋升为陆军少将（全世界哪有这么个军事升级法的）。发动西安事变那年张学良才36岁，就已晋升为陆军一级上将，是除了蒋之外，全中国最高军事领袖；另一个是他的东北人性格，在纽约见到张学良聚谈那次，他描述自己说，“东北人有优点，但毛病也很多，鲁莽，好冲动，捅娄子，我正是这种性格，而且人家让我捅一个娄子，我一定捅俩。”这样的知识水平，这么大的军权，又加上这样的鲁莽性格，不出西安事变才怪了。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; ●第四，西安事变造成什么后果？ <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 在西安事变爆发前一年，红军在江西的第五次反围剿失败，被迫进行两万五千里长征，其间兵员损失惨重。在西安事变前夕，另立中央的张国焘又带几万红军出走，毛泽东的陕北红军仅剩下不足两万人，这时围剿红军的国民党中央军、张学良的东北军、杨虎城的西北军加起来有33万人，红军处于空前的险境。但西安事变改变了这一切。共产党军队不但不再被围剿，而且被正式承认为国民政府军队，建制八路军和新四军。在八年抗战中，毛泽东实行“三分抗日，八分发展”的内部政策，至抗战结束时，两万人的红军发展到八路军90万，新四军30万，共产党有了120多万军队。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 日本历史学家松元一男在《张学良和中国》（台湾新潮社1992年版）中说，“在西安事变里获益最多者是中国共产党，蒋介石失去了身为国家元首的面子，张学良种下了被监禁半个世纪以上的开端，杨虎城也导致了被惨杀的命运，但是对共产党来说，拜西安事变之赐，红军得以苏生、发展而取得天下。”“中国共产党之所以有今天，是托张少帅之福。因为认清这个事实，中国共产党一直把张学良看成‘千古功臣’。” <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 对西安事变导致共产党和红军幸存这一事实，史学家几乎没有异议；那么关键是人们怎么看待共产党和红军的幸存，和后来获得政权。如果认为最后坐大并至今掌权的共产党给中国人带来了民主、自由和幸福，那就应该肯定张学良；如果认为共产党给中国人带来的是专制、灾难和痛苦，那就应该否定张学良。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 当然，没有西安事变，后来抗战爆发，共产党也可能东山再起。但西安事变给了红军一个喘息和幸存机会已是公认的事实。因此胡适说：“没有西安事变，共产党很快就可以消灭了……西安事变对我们国家的损失是无法弥补的。” <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 在事变第二天，胡适就曾给张学良发去一封电报指出：中国形成一个领袖不容易，如果蒋介石出现不幸，中国将倒退20年；并严词警告张学良“应念国难家仇，悬崖勒马”，陪同蒋介石回南京，以谢罪国人；同时明确定性张学良发动西安事变是“名为抗敌，实则自坏长城”，是“国家民族之罪人”。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; “中国通”费正清在他的最后一本著作《中国新史》（China: A New History）中对此写到：“若不是日本全力侵略，南京政府本来可以逐步引导中国走向现代化。事实却不然，抗日战争给了毛泽东和共产党机会，他们在乡间奠定了新的独裁势力，却排除了国民政府刚刚开始发展的都市文明社会的影响，在战争环境下，中国共产党建立起一个准备好打阶级战的新形态的中国。” <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 西安事变的第二个后果是，蒋介石被迫提前全面抗日。由于仓促，结果只能用中国人的血肉铸成“新的长城”。黄仁宇在他的书中感叹说：“世界上没有一个以农村社会作基干，不具实质上的统一，衣食未果的国家，打败一个工商先进国家的先例。”结果是，面对日本侵略军的飞机、坦克、大炮，中国军队根本没有打击的武器，士兵只得全身绑上手榴弹，滚到坦克底下，和它同归于尽。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; “战事既开之后，中国即发现不仅航空汽油全赖输入，空投炸弹亦不能自制，一架飞机缺乏轮胎即不能起飞。……淞沪战役历时十周，中国损耗了85个师的兵力（近50万人！），整个防线暴露在日本海军大炮射程之内……徐州战役之后，中国只能以黄河决堤长沙大火等方法迟滞日军……” <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 那份力量悬殊的惨烈，那份艰苦卓绝的悲壮，回荡于整个抗战过程。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; ●第五，谁应该对西安事变负责？ <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 西安事变是中国近代历史中最具戏剧性的事件，因为中国两千年历史，从没有过这种方式的兵谏。兵谏等同于军事政变，而政变不是取而代之，就是使最高领袖成为傀儡，因为无论从常识或逻辑上，在武力威逼下即使被捉者改变政见，一旦重获权力，绝不可能接受这种方式。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 但张学良就是这样一个不按逻辑，不遵常识，不想后果做事的人。在捉蒋前的师军长会议上，他宣布要动手，众人无语，只有王以哲军长问“捉了之后怎么办？”张学良回答，先捉了再说。这么大的事怎么可以先捉了再说？而在张决定陪蒋返南京时，部下劝阻，他又是先送他回去再说。天底下像张学良这样胡涂透顶、又获得这么高军政领袖地位的，恐怕找不出第二个，这也是中国一绝。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 著有《张学良侧写》一书的台湾作者郭冠英是张学良的崇拜者，和张“往来甚密”。但他也表示，张学良“是公子哥性格，没常性，做事冲动，毫无计划，与这种人共事，‘做他长官头痛，做他部下倒霉’…… 结果是‘害朋友，毁部属’。”（美国《世界日报》周刊1996年12月29日） <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 但说张学良做事全无计划也不尽客观，大陆发表的史料已证实，在西安事变之前，张学良曾秘密和周恩来等共产党人见面会谈，提出“你们在外面逼，我在里面攻，内外夹攻，把蒋扭过来”的方案。据上述朱永德研究西安事变的论文，“张学良曾认真的要求加入共产党，如今共产国际否决他入党要求之文件也已刊出，足见张与共产党之间所有之关系已非寻常。”因此西安事变发生后，张学良“等待来自莫斯科的是赞扬和无尽的军事援助。”这位在论文中一口一个“毛主席、周总理”的中国历史教授也承认，当时张学良认为捉蒋、联合红军，苏联就会高兴，给予军援。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 但完全出乎张学良意料的是，苏联竟闻讯大怒，苏共《真理报》和《消息报》相继发表社论抨击张杨的举动，认为这将导致中国内战，日本坐收渔翁之利。已证实的史料是，事发第四天，苏共给陕北发去电报，要求和平解决西安事变。美国记者斯诺的《红色中国散记》中也说，宋庆龄曾告诉斯诺，这期间她“曾替斯大林转过一封很凶的电报给毛泽东，内容是要求中共必须（对张学良）施加压力放蒋。”张国焘的回忆录也提到当时曾接到斯大林措辞很严厉的一封电报。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 大陆公布的当时代表共产国际的苏共领导人季米特洛夫的日记有这样的记载：“张学良的行动，不论他的意图如何，客观上只能损害把中国人民的力量团结在抗日统一战线中，并会鼓励日本对中国的侵略。”（华谱：《中共党史最新资料》） <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 毛泽东们本已准备在延安搭台子公审蒋介石，但迫于苏共压力，只得派周恩来去劝张学良释放蒋介石。张学良最后释放蒋，也是在众叛亲离，除了杨虎城之外没有任何外援下不得已做的选择。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 在纽约那次聚谈时，当提到周恩来，张学良仍夸周是“大政治家”，“说话简洁，反应迅速，非常机敏。我们在一起谈事情，三言两语就能谈清楚。”但他也抱怨说，当初周恩来说捉蒋，讲得头头是道，后来说放蒋，也是头头是道。其实关键不是周“头头是道”是“大政治家”，而是张学良“头头无道”是“小土匪头 ”，别人怎么说，他就随着往哪边走，全无自己头脑。张学良在聚谈时也承认，他是东北的“白帽子”（过去东北赶大车的人都戴白毡帽），这是东北土话，相当于北京话“二百五”，广东话“漆线”。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 西安事变后蒋介石评价张学良是“小事精明，大事胡涂”。张学良在纽约聚谈时则评价蒋介石“有大略无雄才”。当年之所以发生西安事变，张学良当然应负主要责任，但这也和蒋介石“没有雄才”相当有关。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 东北不抵抗是蒋、张共识，由张一个人担负“不抵抗将军”污名，蒋不但不予安慰，还派悲壮地唱着“我的家在东北松花江上”的东北军去剿共，损兵折将又不补充，怎能不让张学良不满生疑。张学良几次向蒋提出抗日，蒋从不透露他的长远抗战计划（不告诉别人，起码该告诉一下陆海空军副总司令的张学良吧），还像对儿女一样训斥他“等我死了你再抗日！”“你给我好好读书！”当手下特务告知西安可能有变让蒋尽快离开时，蒋竟自信满满地说“他敢！”连宋美龄后来都批评丈夫“不知安抚，刺激生变”。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 而且蒋介石既然认为张学良“误了我的大事”，但回到南京后竟愚蠢地遵守在枪口下同意改变“让外必先安内”政策的口头承诺（没有文字），承认红军为国民政府部队，授予番号，颁发军饷，宁可大事被误。在他明确认定共产党是“匪”的情况下，再按被匪枪口威逼之下的承诺去做，就根本不是值得称赞的“君子 ”，而是“傻子”。如果说张学良是“二百五”，那么蒋介石这种刚愎自用、同样“大事胡涂”就是“二百六”，这样的正、副司令主导下的国民党怎么可能不丢失大陆？ <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; ●第六，张学良为何近一生被囚禁？ <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 张学良陪同蒋介石抵达南京后，受到军法审判，以“劫持罪”判处有期徒刑10年。但第二年张学良就获特赦，随后被长期“管束”，实则幽禁，直到蒋家父子去世、李登辉担任总统时才获得完全的自由。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 西安事变，不仅使蒋介石丧失颜面，还有政府高官及蒋的几乎全部卫士被打死。从蒋介石的角度，这种行为显然于国民政府之法律、于中国文化之伦理、于长官下属关系之情理，都完全不容。因而当时军法审判张学良，获得国人支持。傅斯年等知识分子当时还提出“对张惩处极刑”。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 但蒋介石为人话诟的是，他不是使用法律手段，而是使用“私刑”，特赦后一直不释放，终生软禁张学良。如果蒋介石认为张学良触犯法律构成死罪或终生坐监罪，应该当时判处死刑或无期徒刑，而不应以家法代替国法，视法律为儿戏。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 有人替蒋介石辩护说，当时如果释放张学良，他和东北军就可能成为共产党的力量；而在去了台湾之后给张学良自由，他就可能像李宗仁那样寻机投共，成为攻击和统战台湾的力量。但这些辩护仍是侧重策略层面，而不是以法律为准绳；而且都是以假设的可能来剥夺一个在法律意义上已无罪的人的自由。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 蒋介石处置杨虎城的方式更是用私刑，秘密逮捕后，不予审判，长期关押，最后撤离大陆时在狱中把杨虎城和他的幼儿杨拯中处决。而且杨的秘书宋绮云全家也是关押多年后被处决，即使宋的孩子（大陆《红岩》一书中“小萝卜头”的原型）也被杀死。如果说杨及秘书是共产党人该处决（也应通过法律审判），但两个不到10岁的孩子有什么罪？这完全是封建皇帝那种“满门抄斩”的做法。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 有意思的是，张学良到死都不怪罪蒋介石长期软禁他。在纽约聚谈时，张学良说，“我几十年失去自由是应该的。如果我是蒋介石，我会枪毙了张学良，因为这是背叛啊！但蒋先生让我活下来，这是蒋先生的宽大。我一生最痛苦的事是蒋先生杀了杨虎城，因为应该杀的是我。” <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 张学良接着还提到，他父亲张作霖曾耳提面命，训诫“做军人要置生死于度外，要把脑袋挂在裤腰带上，对此，学良一生不敢有忘，早把生死视同平常。军人叛变是死罪，但我却活了下来。40年失去自由是公平的。” <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 张学良获得自由来到美国定居后，北京政府多次捎话邀请他回大陆看看。在纽约聚谈时，张学良透露说，他在台湾的一个侄女去大陆，曾见到张学良的东北 “讲武堂”同学吕正操，吕捎话邀请张学良回大陆探亲访友。吕曾担任中共将领和铁道部长，文革中，吕和张学良的弟弟张学思一起被打成“东北帮”首领关在一起。吕对张学良的侄女说，当时他为保命过关，什么都交待，让承认什么就承认什么；但张学思拒不认罪，反而高呼冤枉，结果遭红卫兵痛打，越打他越喊，最后因斥骂红卫兵被活活打死。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 张学良还透露说，在北京办亚运会时，当时任国家主席的杨尚昆特地邀请他在台湾的那个侄女等家人赴北京，参加“亚运会”开幕式，坐在贵宾席。会后杨尚昆亲自接见了他的侄女，并捎话给张学良，只要他愿回大陆看看，他可派专机到台北接他。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 张学良在透露这些内情后说：依我本愿，很想回大陆去看看，主要是想看看东北乡亲，给父亲扫扫墓；但我最怕卷入争执纠缠之中。我已远离政治，唯一的愿望是大家把我的名字忘却，只当一个普通的小小百姓，从此不再有政治纠缠，不再有记者踏破门坎，不再有任何采访，求闲云之清静，达野鹤之超脱。因此他给自己起的新名号是“闲云野鹤”。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 张学良的老同学吕正操后来还专程飞来纽约，面劝张学良回大陆，但张仍是至死也没有回去他曾说过“想极了”的家乡，而死在海外。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 张学良为什么选择不回去？从纽约聚谈的印象，以及张学良的其它谈话可以感到，张学良对当年那种劫持蒋介石的方式已痛悔，他多次说自己“鲁莽冲动” 即是委婉表达后悔之意。在纽约时还明确说他发动西安事变是“闯了大祸”，自己是“罪人”。他清楚共产党感激的正是他发动西安事变，使红军得以幸存最后掌了天下。如果他回到大陆，共产党当他的面捧他为“大功臣”，他会很尴尬；但如果他真实道来对发动西安事变的悔恨，也会使中共下不了台。与其双方尴尬，不如暂时避开，这可能就是张学良一再说等政治不再纠缠时再回大陆的真实心理。但中国人的事情，什么时候会没有政治纠缠呢？ <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; ●第七，张学良怎样看待中国那段历史？ <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 《张学良侧写》作者郭冠英说，“张学良这个人，大陆把他捧得太高，台湾又把他贬得太低。”台湾把张学良贬为“千古罪人”，主要是指西安事变导致共产党最后坐大。但没有西安事变，是否共产党一定在中国消失？从蒋介石的刚愎自用，独裁本性，以及国民党政府的腐败无能，很难下这种绝对判断。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 但大陆把张学良捧为“抗日民族英雄”则完全是从政治而不是事实出发。因为连张学良本人看了蒋的日记也明白了，蒋介石不仅要抗日，而且有详尽的战略计划。因此西安事变并不存在促使蒋介石抗日这回事，而只是把原来计划的争取时间、积蓄力量的战略部署破坏了；把全面抗日的时间表提前了。这除了对日本人有利，对中国人则是巨大的灾难――抗战的惨烈已完全证明了这一点，因为中国从各方面都准备不足。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 史学家许倬云曾对此评论说，“当时中国在选择抗日的时机上还是太急了一点，假如能再延迟五年，情况很可能完全不一样。”这位芝加哥大学历史学博士所以提出“延迟五年”就是因为1941年珍珠港事件美国对日宣战之前，中国的抗日战争基本没有得到国际社会的支持，实在是孤军血战，牺牲太大，八年抗战中国军人阵亡300万，平民丧生1,000万。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 张学良发动西安事变，导致中国被迫仓促提前全面抗日，对中国人、中华民族，都有害而无利。表面看张学良好像是“爱国”，但其实是害国。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; ●张学良是“假英雄” <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 当然，以张学良的学识和认知能力，当时他完全没有可能预见到共产党后来会建立那样残忍、专制的政权，不要说张学良这样没有什么知识的人，那些大名鼎鼎的知识分子们，不是也有很多都选择追随共产党了吗？ <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 关键的是今天张学良对共产党的认识，对当年他发动西安事变使红军幸存对改变中国历史进程的责任的认识。虽然不能说共产党在大陆坐大就是由于西安事变，但毕竟它是一种很大的可能性，而在中共统治下可能多达八千万中国人因迫害和饥饿丧生。但张学良对这些都无动于衷，更无任何忏悔表示。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 1994年张学良在接受陆铿采访时还斩钉截铁地说，对于西安事变“我愿负全部责任，而且从不后悔。”（香港《百姓》半月刊1994年5月1日） <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 在纽约聚谈那次，张学良仍表示蒋的“攘外必先安内”政策是错的，他的联共抗日才是对的。如果蒋当年没有采纳张的意见，张以自己的政见没有得到机会实行而批蒋还有逻辑可言；但事实是蒋已被迫实行了张的主张，联了共也抗了日，结果不仅红军幸存，共产党还最后坐大，至今涂炭大陆生灵。张从哪里得出他的“ 联共抗日”是对的结论呢？他总不至于希望见到共产党坐大吧？但这就是张学良的“可爱之处”，一生“大事胡涂”，胡涂死了！因此台湾作家柏杨断言：“将来无论是统是独，张学良都不是英雄。” <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 陆铿撰文说张学良“对得起中国，对不起自己”。但事实是，在中国现代史上，除了共产党人之外，没有谁比张学良更对不起中国了！ <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; ●张学良是“假将军” <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 自张学良继承父业，就被称为“少帅”。中国方块字这种象形联想，使人想到英姿勃发、统帅千军万马打胜仗的将军。但事实上张学良是个“假将军”。他的崇拜者郭冠英在文章中也承认，“918之前他又打毒针又好女色，许多人对他的印象坏透了。” <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 这个公子哥是因父亲被暗杀而获掌东北军权的。他曾指挥东北军五个旅团和苏俄军队交手，结果全军覆灭；他指挥东北军陕北剿共，直罗镇、榆林两战，张的两个精锐师两万多人被红军全歼；他指挥热河保卫战“只到前线去过一次，还是为了陪宋子文；汽车每行15公里就得停下来，让他注射毒品……他每天总要打上百支毒剂；而这种毒剂，假如给不吸毒的普通人一次注射10支，即使不死，至少也要重病一场……有次召开紧急军事会议，他竟把下达的军令公文随便揣在大衣口袋，忘了发出去。他自己说，当时散在前线的各个部队，他连其位置都搞不清楚，还谈什么指挥作战！”（毕万富：“从新发现的史料谈张学良的抗日主张”四之二，美国《世界日报》1996年1月16日） <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 《纽约时报》在张学良去世后刊发的长篇讣告中也毫不客气地指出，张学良虽然有20万东北军，但他沉迷于毒品和女色，是个出名的“花花公子”。他的心思不在抗日，而是在和墨索里尼的女儿（当时意大利驻中国公使的妻子）打情骂俏等社交活动。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; ●张学良是“假基督徒” <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 1955年，张学良在宋美龄的劝导下皈依了基督教，但其实他是个“假基督徒”。基督教的核心教义之一是忏悔，但张学良对做过的错事却从无真正的忏悔。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 多次采访过张学良的郭冠英说：“其实张学良从未忏悔，他当然不好大声说他做对了，他只好说‘做了就做了，没什么忏悔的问题’…… ”（引文同上） <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 在纽约聚谈中，当《东方新闻报》主编李勇提到在中共统治下中国人丧生数量远超过抗日战争时，张学良的回答竟是，“哪个政府为维持政权总是要杀些人的。”当我提到刚过去不久的“六四”屠杀时，张学良竟说，“电视我没看，对这事不太清楚。……什么事不能只听一方的。”对“六四”这个世界已经定论的事情，张学良竟是这样的看法，天底下哪有这种没心、没肺、没灵魂的基督徒？ <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 蒋介石软禁了张学良半个多世纪，固然是违法和不人道的，但是和无数因大陆落入共产党手中而遭迫害致死的国民党人相比，张学良实在是过着潇洒的天堂般的日子。他不仅不需要像黄维、杜聿明们那样去做体力劳动、不需要受人身污辱、不需要接受思想改造，更没有任何衣食住行上的生活担忧，在软禁的头三年里还有一妻一妾轮流陪伴（如果不是由于夫人于凤至因病赴美的话，这种日子大概起码可以维持到张信“基督”的六十年代）。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 不知道在这漫长的几十年里，在张学良平静的日子里，即使他从未思考过其它中国人的命运，他是否想过有多少国民党将领、士兵的家庭被台湾海峡隔了半个世纪，撑过各种磨难熬过来的幸存者们，重逢相见的时候已是面目皆非了。而他的赵四小姐，虽然青春不再，但仍然是那个在身边照料左右、那个熟悉的她。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 即使张学良全然不在意其它留在大陆的国民党人的命运，他怎么也应该记起他的亲弟弟吧？我想起在78或79年的时候在大陆曾读到过一篇回忆张学良的亲弟弟张学思的文章，文中说，张学思在文革中被关押、拷打，生命垂危的时候，想念东北食物，希望狱方能给他吃一点东北的苞米（玉米）楂子和土豆，但就这么一点小小的要求竟然在他临死的时候也被拒绝了。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 张学良说“哪个政府为维持政权总是要杀些人的”，不知这该杀的人里面包不包括张学思？如果他对八千万中国人的死亡没有感觉的话，对自己的亲弟弟遭如此非人折磨，并被活活打死也毫无感觉吗？ <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 在张学良所谓“传奇”的一生里，他和赵四小姐的“爱情”也是最为人津津乐道和关注的。但在张学良恢复自由来美后接受“美国之音”采访时，在被记者重复追问“您和赵四小姐这段深刻的爱情”、“她真的是您生命中不可缺少的支柱吗？”“您觉得她怎么好？”这些读者最关心的问题时，他的回答竟是：“当年她年轻时，也是个很好玩的小姐；陪我这些年也实在不容易。”原来赵四（不是三陪小姐）只不过是个“专陪小姐”而已。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 这还不够，他还在91岁高龄时对东北同乡祖炳民说：赵四小姐赵一荻对他最好，将一生都奉献给他，但不是他最爱的女人，“最爱的女人在纽约”。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 据最近台湾《中国时报》电子版“张学良和他的四个女人”一文，这四个女人是：他的母亲、蒋夫人宋美龄、于凤至和赵一荻。明显地，那个“在纽约的女人”指宋美龄。像张学良这种无知、鲁莽、玩腻了“小姐”的土匪头子，崇拜受过美国教育，又表现出雍容高雅的宋美龄倒不足为奇；但蒋夫人是否像媒体渲染的那样“关照”张学良则是另一回事儿了。张学良首次来美国在纽约呆了几个月，连我们这种毫不沾边的小老乡都见了，怎么就没有媒体报导说他见了一直住在纽约、近在咫尺的宋美龄呢？应该不是他不想见“最爱的女人”吧？而且在他死后宋美龄也没有任何公开的表示。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 陆铿曾对我说，赵一荻告诉他：她感谢蒋先生，因为没有蒋先生的“管束”，张学良的身体早被“掏”空了。张学良即使到了晚年，仍是“性”趣盎然，逢人就炫耀他的好性本色。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 男人好色是天性，既不是奇闻，更不是英雄之举。但跟到处炫耀好色的男人生活在一起的女人真是天下最不幸的女人（当然张学良也有情可原，他活到一百岁，除了好色，没有任何其它值得炫耀的东西）。没想到陪伴了张学良73个年头（其中35年没名份）的赵一荻女士竟这么可怜，他从张学良那里得到的评价只不过是个“很好玩的小姐”，陪他玩了一辈子，最后人家最爱的还是别的女人。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 张学良在晚年热衷什么？1994年2月一群华人在夏威夷同张学良聚会时，张自我总结说：“我已经93岁了，我这一生有三爱：爱打麻将；爱说笑话；爱唱老歌。只要有得玩，我就不累。”这就是被中国人视为“民族英雄”的张学良――全然一个市井村夫，而且是活得最俗气、最胡涂、最没灵魂的那种。从 1936年到2001年，65年的生命在推麻将中推掉了，活到超过一百岁，什么人生道理也没活明白，真是白活了！ <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 惟有那首张学良给无数人题了无数遍的打油诗，倒可能是他一生唯一“明白”的自我画像： <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 自古英雄多好色， <br />
&nbsp; &nbsp; 未必好色尽英雄； <br />
&nbsp; &nbsp; 我虽并非英雄汉， <br />
&nbsp; &nbsp; 惟有好色似英雄。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 2001年10月24日于纽约<br />
曹长青</div>

							
							
							
							
