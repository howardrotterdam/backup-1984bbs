比如说：<div class="quote"><h5>引用:</h5><blockquote>原帖由 <i>鹏鹏</i> 于 2010-7-26 18:04 发表 <a href="https://1984bbs.com/redirect.php?goto=findpost&amp;pid=759680&amp;ptid=54005" target="_blank"><img src="https://1984bbs.com/images/common/back.gif" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /></a><br />
