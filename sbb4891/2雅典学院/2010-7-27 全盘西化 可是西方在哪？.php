<br />
――问“为什么不能搞民主，还权予民呢？”<br />
――答“即便把选票给你又怎么样？你有能力用好嘛？再说，那些权力是属于人民的，不是属于你个人的，我们党戴三个表，把人民的权力放在能用好的地方就好，别太狭隘。”<br />
<br />
这就导出了一个大部分小组众不喜欢的威权+民粹的结论。<br />
<br />
<br />
回到鹏鹏的问题，“西方的就一定是经典的么? ”，这个问题提得真是让人纠结，完全无法给出明确定义再讨论的问题。<br />
<br />
我以为，鹏鹏的原意是要问“西方的（观念）（对中国人来讲）就一定是正确的么? ”。<br />
<br />
要回答这个问题，首先得明确，鹏鹏所指的究竟是什么观念，我以为，这当中存在着一个区分实验层面的观念和价值层面的观念的问题。<br />
<br />
PS:当然也有人认为不存在实验层面和价值层面的区别，如相信“科学方法能解决一切问题”的科学主义者们，自由主义者中最有名的例子当属胡适。对这种&quot;人僭神位”的认识论，后现代主义思潮多有批判，哈耶克对人类理性的深刻怀疑亦言犹在耳。<br />
<br />
在实验层面上，我们可以说，“西方”和“东方”分享着相同的观念，绝没有西方的一加一等于二，东方的一加一偏偏等于三的道理。<br />
<br />
在价值层面上，今天源起于“西方”的个人主义的价值观已籍经济的力量席卷全球，它的对错当然应该讨论。<br />
<br />
<br />
<br />
我想说的是，“西方”的概念，是中西对立的二元思维的产物，清末出洋考察的五大臣都能意识到老太后喜欢德国、日本的君主立宪而不喜欢法国的共和，今天的人们却反不能区分了么？<br />
<br />
其一，不应该把“西方文化”当成一支独立于世界其他文化发展的文化。其二，不应该把“西方”当作是一个具有内部同质性的实体。<br />
<br />
一，虽然大致可以说&quot;西方国家”的文化都来源于希腊-罗马文化，但近东的希伯来文化，埃及文化，以及其后的阿拉伯文化，这些今天看来是“东方”的文化都给予了“西方文化”以重大影响，马克思曾说“火药、指南针、印刷术――这是预告资产阶级社会到来的三大发明。火药把骑士阶层炸得粉碎，指南针打开了世界市场并建立了殖民地，而印刷术则变成了新教的工具，总的来说变成了科学复兴的手段，变成对精神发展创造必要前提的最强大的杠杆”即是文化间交互影响的一例。<br />
<br />
二，龙生九子，各有不同，谁能证明个别“西方国家”间的差别就一定小于个别“西方国家”与中国间的差别呢？谁能证明“西方国家”在重大问题上的观点具有根本性的一致呢？<br />
<br />
举几个例子：<br />
<br />
就哲学趋向上来说，大致有欧陆理性主义与英国经验主义的区分；<br />
就经济学理论上来讲，古有重商学派与重农学派的争执，晚近有社会主义、凯恩斯主义与奥地利学派的笔战；<br />
就宗教上来讲，天主教和新教曾经打得不可开交，而新教内部，路德宗和加尔文教派也是互相不认账；<br />
<br />
<br />
<br />
我亲爱的自由主义小斗士们，胡适的确赞成过“全盘西化”，但他讲的是在学习西方文化时不要预设底线，须用“大胆地假设，小心地求证”的方法，并不是一概地以西为尊。<br />
<br />
仔细看来，“西方的就一定是经典的么? ”中体现的怀疑立场，不是比某些急着替殖民主义护短的做法更符合胡适先生的期许么？<br />
