主持人问：最近在中国很流行对朝鲜的诋毁和诅咒。但是孔老师曾经在自己的博客上为朝鲜说了公道话，而且2006年曾到朝鲜大使馆做客。最近呢，也因为这个问题又给您带来了一个很意外的麻烦，我也在博客上看到了。我想听听你对朝鲜和这个问题的看法。<br />
<br />
孔庆东：这个问题我觉得非常奇怪。朝鲜做了什么对不起人的事了？有人说我说了公道话，我觉得这个有点抬高我了，因为我不知道朝鲜做了什么错事。她是在公路上随便驾车压死人了，是骗别人的钱了，抢了别人的东西了？朝鲜在这个世界上做了什么对不起别人的事了？她没有！她在别的国家驻扎过一兵一卒吗？她破坏过任何一个国家的市场、经济、环境和资源了吗？她什么都没有干啊！在社会主义阵营崩溃之后，她一个人艰难地过日子。原来是兄弟们大家一起过，后来兄弟们垮台的垮台，叛变的叛变，剩下她一个还在那里坚持着，还按照大哥二哥原来的路线在走。她没有这种自由吗？她有什么错，她没有什么错啊！她也没得罪谁啊。好人没得罪，坏人也没得罪。她对坏人够客气的了。她没有做什么坏事。但是不知怎么回事得了那么多的恶名。而所有这些恶名都是没有经过实地调查研究的。<br />
<br />
说她是流氓国家的人，自己是这个世界上最大的流氓。（掌声）她有什么流氓行径啊？这个世界不是要强调民主自由吗？民主自由就是不干涉别人的生活方式。人家朝鲜人民选择那样的生活方式，我们对她不满意――我自己对她也不满意，我也觉得朝鲜这样过日子好像有问题，但是我没有权利去说人家不对。我们怎么敢对别人家的生活方式指手画脚呢。人家就愿意吃大米，你说我们家愿意吃白面，非说人家吃大米不好。或者人家就愿意吃辣椒，你不能说吃辣椒就是野蛮的。人家并没有把辣椒推到你家里来强行让你家吃啊！人家在你家里到处开什么朝鲜凉面、平壤烧烤、上甘岭泡菜了吗？她没有啊！她没有做什么过分的事情。<br />
<br />
如果真的对她好，你应该以朋友的口吻去给她提建议，而不是天天在她家门口贴大字报。而贴大字报这些人呢，都不了解人家。我也不大了解人家，但我知道什么不是真的。我只知道朝鲜不是那样的。从小我家周围就有许多朝鲜族的邻居朋友，我所知道的这个民族是很好的一个民族，是最有文化的一个民族。在中国56个民族中是文化水平最高的一个民族。我到韩国之后发现，韩国人都被洗脑了。他们天天说朝鲜不好，天天说朝鲜要侵略他们。可是我生活中没有感到这个迹象啊！有一篇文章里我调侃韩国饮食很恶劣。天天吃米饭就泡菜，吃泡菜就米饭，还说朝鲜不好。就算朝鲜再不好，还能恶劣到什么程度？难道说天天只吃米饭不给泡菜还是只吃泡菜不给米饭？人们不去反思，只要反思一下就会明白，这个北朝鲜她没有做什么过分的事情。我觉得如果你真对她有什么意见，应该是在一个共同的思想背景下来探讨。否则其实就是为了国家利益树一个敌人，为了团结自己国家的人民，在境外树一个敌人，而且树一个好打的敌人，柿子拣软的捏。我不赞同这种做法。（掌声）</div>

							
							
							
							
