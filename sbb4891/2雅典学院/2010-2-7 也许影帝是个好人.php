l表rg：1/6/2007<br />
<br />
二六年“五一”期间，胡锦涛赴青岛视察海军，乘驱逐舰行驶于黄海海面视察海军演练，胡锦涛的座舰忽然同时遭两艘海军驱逐舰机关炮扫射，险些丧命，胡身边的五名海军士兵被打死。事发后，胡锦涛仓皇逃离青岛，飞往云南，喘过气来之后，就开始了上海的“反腐风暴”......<br />
<br />
中共党国领导人出访，行程之隐秘，保安措施之严密，远远超过美国等民主国家领导人，在如此严格精细的安全护卫准备下，海军是不可能发生这样离奇的“意外”的，黄海事件显然是一次刺杀胡锦涛行动。<br />
<br />
在黄海刺杀行动中，两艘驱逐舰同时向胡锦涛的座舰开火，配合之默契，显露出这是一次精心策划的暗杀行动。要精心策划对胡锦涛的暗杀，必须事先要详细了解胡锦涛赴青岛视察活动，由此可以推断：刺杀胡锦涛的幕后指挥者，必然是一位事先详细了解胡锦涛赴青岛视察活动的人。<br />
<br />
但是，中共领导人出巡，日期和行程、巡视地点、会见的人物、驻地、停留时间、出访交通工具等等事前一概严格保密，要公布也是总是事后才公布，而且，中共领导人视察的地点事先都要进行严格的清场和戒严，任何能够接近领导人的人，乃至包括欢迎的“群众”，都要经过审查和精心挑选，这种不惜侵犯人权的保卫措施，使得行刺中共领导人远比行刺美国等西方国家的领导人困难。<br />
<br />
只有极少数中共最高层成员才能够事先详细了解胡锦涛赴青岛视察活动，因此，刺杀胡锦涛的幕后指挥者，必定是极少数中共最高层成员之一。<br />
<br />
黄海刺胡事件传出后，海内外观察人士一致认为江泽民是谋刺行动的幕后指挥者，我却不以为然，我以为，这次谋刺事件的幕后指挥者是曾庆红。<br />
<br />
江泽民的确具备谋刺胡锦涛的条件：老贼虽然已经没有职务在身，但是其退而不休，而且江系人马在党内、军内高层仍然占据要津，在地方更是人脉广泛，因此江泽民政治能量极大，他仍然是中共最高层的大佬，他必然能够事先知晓胡锦涛这次视察活动。<br />
<br />
但是江泽民并不具有要干掉胡锦涛的强烈动机，这是因为：<br />
<br />
一，在黄海谋刺之前，胡锦涛的作为并没有对江泽民的人身安全构成任何威胁，也没有流露出要清算和威胁江泽民的征兆。胡锦涛于二二年就任总书记以来，对江泽民处处忍让，在二四年逼江交军权的行动中也没有牵头。胡锦涛对退职后的江泽民给予高规格待遇，也不准清查因周正毅案暴露出来的，江贼民太子的惊天贪腐罪行；四年多来胡锦涛高调肯定江贼民的“政绩”，胡锦涛一直卖力地扛着江老贼留下的镇压法轮功的沉重的政治包袱，对老贼留下的千疮百孔的政治烂摊子在公开场合几乎没有任何微词。胡锦涛就任总书记以来，虽然有将自己“八荣八耻”、“和谐社会”的“理论”取代江贼民 “三个代表的倾向，但是“三个代表”已经写入党章和宪法，是胡锦涛难以否的。<br />
<br />
二，在黄海谋刺之前，胡锦涛也没有清算在职的党、政、军高层江系人马的行为和征兆。黄海事件之前，江泽民亲信，海军副司令王守业因为贪腐和淫乱，数年来遭揭发控诉的状纸如麻，地位却稳如磐石，这显然是胡锦涛在袒护；江系干将，上海市委书记陈良宇，多年来在上海滩肆意横行，贪赃枉法，民怨早已冲天，胡锦涛也没有清查的动作和意思；十六大后窜升至政治局常委的贪官奸佞李长春，因其在河南、广东期间的贪腐劣迹和在艾滋病问题上的严重渎职行为，不仅民怨沸腾，也在中共党内遭到强烈反对，多次被地方干部举报，却一直安然无恙，这显然也是胡锦涛在袒护；江系亲信，超级贪腐分子贾庆林、黄菊在党内都声名狼藉，下台的呼声很高，黄海事件之前，他们的地位也稳如泰山，这显然是胡锦涛袒护的结果。胡锦涛还一直对与江贼民关系亲密的杀人犯罗干、周永康委以重任。可见，胡锦涛不仅没有清算在职的党、政、军高层江系人马的行为和征兆，还有袒护江系人马的动作。在黄海事件之前，胡锦涛对江系人马一定程度上的袒护，大概是为了制约曾庆红的势力，搞权力平衡。四年来胡锦涛虽然在地方上大量安插团派人马，但这并不能对江系势力构成致命的威胁。<br />
<br />
三，一旦胡锦涛被干掉，江贼民并不能成为最大的受益者。按照中共黑帮现行帮规，胡锦涛如果在任上发生意外，曾庆红将自动接任总书记、国家主席和军委主席，最大的受益者是曾庆红。虽然曾庆红曾经是江贼民的智囊和助手，但是也正是曾庆红在二四年秋出卖了江贼民，使得老贼继续霸占军委主席一职的企图落空，江、曾由此结怨，江泽民决不会再信任曾庆红，与谨小慎微、四平八稳的胡锦涛不同，曾庆红为人更具野心、胆魄和手腕，对于江贼民来说，一旦曾庆红掌握了中共的最高权力，自己的前途命运更加凶险，因此，江泽民决不会支持曾庆红取代胡锦涛。江泽民要想通过除掉胡锦涛而成为最大受益者，唯一的做法就是再刺胡的同时发动军事政变，让陈良宇或贾庆林或李长春或者自己的儿子坐上中共最高统治者的宝座。不过，要举行这样大的动作需要权威和对军队，特别是北京卫戍部队的牢牢掌控才行，江泽民在军中威信不高，他在军中的人脉虽广，但没有对军队的绝对操控力，江泽民也没有鼓动军队的先进的理念，因此，江贼民不太可能敢于发动推翻胡锦涛地军事政变。而且，要举行这样的大动作，肯定要准备二套方案乃至数套方案，从黄海事件来看，刺胡的行动者在击胡不中之后，没有后续动作，比如说拦截或击落载着胡锦涛逃窜的直升机（直升机是容易被击落的）、军队起事、封锁青岛机场等等，这表明黄海事件只是一个单纯的刺杀事件，不是一次政变。<br />
<br />
第一、二点原因决定了江泽民没有干掉胡锦涛被迫性，第三点原因决定了江泽民没有除掉胡锦涛的主动性。<br />
<br />
与江贼民的情况相反，曾庆红具有除掉胡锦涛的压力和强烈的驱动力。从被迫性的因素来说，当前中共政局的现状如果维持到十七大不改变，曾庆红不仅再也不可能登上中共国的的最高权力宝座，连保住现有的权力和地位都困难重重：出生于一九三九年的曾庆红长胡锦涛三岁，到明年十七大时已年满六十八岁，按照中共十五大以后形成的“七上八下”的黑帮帮规，曾庆红理当退休。虽然现在胡锦涛没有流露要曾庆红退休之意，但若曾某人继续恋栈，必然在中共高层集团里引发激烈的反对。曾庆红为人高傲，行事张扬，反复无常，而且当年紧跟江贼民，飞扬跋扈，这使他与罗干结怨，与黄菊、徐才厚等江系人马不睦，也得罪了某些元老；更何况，曾庆红本人正是“七上八下”规矩的始作俑者，“七上八下”的规矩为曾庆红提出，这个规矩的创立，正是江泽民在十六大上骗取六十八岁的李瑞环退休的手段，哪知道这个规矩今天却成了曾庆红政治道路上巨大的障碍和尴尬，诚可谓“作茧自缚”。曾庆红目前在中共还没有大的权威，如果他拒绝遵守自己提出的这个规矩，等于是出尔反尔，中共高层的反曾势力和曾某人所开罪的那些政治大佬岂能容忍？今年下半年，《争鸣》杂志披露：王兆国和李铁映在今年的人大“党组生活会”联手向曾庆红发难，要求曾辞职，王兆国和李铁映被胡锦涛批评为搞“派别纷争”；《动向》杂志披露：今年九月下旬，上海陈良宇事件爆发后，党内外纷纷将矛头直指向江、朱、曾，十一月上旬，曾庆红突然在中央政治局常委会会议上提出辞程，并且开列了不利于胡锦涛的五个辞职条件，这明显是曾庆红以退为进，要求胡锦涛支持自己连任的胁迫动作，曾庆红的这个动作，既流露出强烈的恋栈欲望，又显露了他面临强大的逼退压力。<br />
<br />
所有这些，都说明，曾庆红连任的困难重重，胡锦涛现在虽然表面上袒护曾庆红，其真实的算盘却不得而知，毕竟曾庆红是头号竞争对手，而可以肯定的是，如果袒护曾庆红的压力会对威胁自己的政治利益，谨小慎微的他决不会再支持曾庆红连任。<br />
<br />
曾庆红会知难而退吗？几乎不可能，因为曾庆红是那种权力欲望极强的人，是一个能够当正，决不甘心为副的人，他决不会轻易地放弃自己好不容易弄到手的权力，曾庆红虽属太子党成员，但曾家与先帝毛始皇关系亲密，不是红朝邓二世时代的当红家族，曾庆红自己韬光养晦、含辛茹苦地奋斗了二十多年，为此不惜投身自己瞧不起的假烈士真汉奸江贼民的门下多年，才有今天一人之下，亿人之上的地位，如今的曾庆红身披国家副主席、中央党校校长的光鲜黄马褂，掌控中央书记处和国家安全局这样的要害和实权机构，离红朝皇帝帝位仅半步之遥，完全是一副“接班人”的架势，这样个人成就，向来为权力不择手段的曾庆红怎么可能心甘情愿地主动放弃？而且，曾庆红得罪过好些中共高层人物，做过好些见不的人的事，包括帮助江贼民毒死杨尚昆，他必然担心一旦失去权力，会遭到清算。<br />
<br />
即使曾庆红施展浑身解数实现了连任常委的目标，因为年龄上对胡锦涛的劣势，他能够通过等待获得红朝皇帝宝座的可能性几乎为零。<br />
<br />
能够使曾庆红摆脱困境的最好的变数就是胡锦涛发生“意外”，一旦胡锦涛发生“意外”，曾庆红可以“名正言顺”地代理总书记、国家主席和军委主席的职务，按照中共的惯例，中共国代理最高领导人只要在代理期间不犯大的“错误”，就会顺理成章地“扶正”，以曾庆红的权谋手段，一旦抓过胡锦涛的职务，到明年的十七大完成对政局的操控并非难事。<br />
<br />
因此，曾庆红具有使胡锦涛发生“意外”的强烈的驱动力，随着十七大的临近，逼迫曾庆红退休的压力越来越大，曾庆红有着除掉胡锦涛的被动压力。<br />
<br />
曾庆红也具备暗杀胡锦涛的一切最有利的条件：他掌管的国安机构，就是专职的暗杀机构；曾庆红掌控书记处，因而完全能够掌握胡锦涛外出活动的计划和情况，另外，书记处也分管军中特情部门；很多人认为曾庆红与军队不合，其实这是误解，曾庆红与某些个军头不合，不等于他在军中没有势力，实际上曾庆红在军中的势力并不弱，比胡锦涛强许多：曾庆红是当前中共政治局中中唯一一个出身军旅的常委，他辅助江贼民多年，在军中人脉很深，与上海帮人马关系复杂。曾庆红的弟妹现今都在军中任职，而黄海刺胡行动的直接指挥者--海军司令张定发，就是与曾庆红关系紧密实力派上海帮将领。<br />
<br />
以上可见，曾庆红如果要行刺胡锦涛，实在是轻车熟路。<br />
<br />
综上所述，曾庆红才是刺杀胡锦涛事件的策划者和幕后指挥者。<br />
<br />
曾庆红为何要选择五一期间胡锦涛视察驻青岛海军的日子下手，一来具备行刺的便利条件；二来，江贼民当时正在大张旗鼓地“东巡”，也刚好“驾临”山东，江老贼效法邓小平，企图以“东巡”的方式打击胡锦涛的权威，维护自己的“三个代表”。这就给了曾庆红杀掉胡锦涛，同时栽赃于江贼民的大好机会：一旦刺杀胡锦涛成功，一切迹象都很容易使世人相信，胡为江所杀。曾庆红则不仅可以“合法”取得帝位，还可以顺势把胀算到老贼头上，对江系人马进行清洗，这是绝佳的一石两鸟结果。<br />
<br />
即便刺杀胡锦涛不成功，一切迹象都很容易使胡锦涛等人怀疑江泽民，从而促使胡锦涛不得不抛开党内“和谐”政策，愤而清洗江系人马，这也可以间接地剪除曾庆红自己在上海帮内的某些对头，还可以坐观胡江两派斗争，从中渔利，或可在变乱当中，争取自己连任乃至称帝的契机。<br />
<br />
黄海刺胡行动的直接指挥者--海军司令张定发是身患绝症之人，事发后，在曾庆红特情系统的暗中袒护下，胡锦涛未必能从张定发口里得到什么有价值的东西，如今人已“病死”，死无对证。<br />
<br />
曾庆红不愧为当今中共最老谋深算的人物，这一切都算计得天衣无缝！<br />
<br />
就后来胡锦涛全力出击上海帮来看，胡锦涛可能还没有怀疑曾庆红，但是，也不能低估了胡锦涛的智商，胡锦涛由一介平民混到总书记的位置，智商不可能低下，即使胡锦涛没有足够的智商察觉黄海事件幕后的曾庆红，不等于他的智囊们不能够察觉曾庆红，胡锦涛现在这样做，也有可能是将计就计：暂且继续稳住和利用利用曾庆红，等收拾了江系人马，再与曾庆红决战。<br />
<br />
不管怎样，可以肯定的是，二七年中共内斗将会恶战连连。<br />
<br />
胡锦涛或许现在还幻想谋求党内“和谐”，这是白日做梦，因为中共从来没有一个权力和利益的理性更迭和调整机制，随着不同利益对立情况的加剧，中共高层的冲突必然呈现你死我活的情况。现在中共党内黄海刺胡事件，就揭开了这种你死我活内斗的序幕。<br />
<br />
黄海刺胡事件不成功的落下了帷幕，并且带着未来中国命运的种种险恶征兆。黄海刺胡行动的干将张定发完全应验了我在二四年秋从姓名对其的预测：<br />
<br />
我那篇于二四年十月五日发表在《大纪元》上的文章《从姓名看中共国命运（之八）――胡温“倾红”（庆红）天难逆 ，三将飙升变仍留》（文址：<a href="http://www.epochtimes.com/gb/4/10/5/n681112.htm" target="_blank">http://www.epochtimes.com/gb/4/10/5/n681112.htm</a>），其中有这样的对张定发的预测：<br />
<br />
“张定发，包含有″一旦拉开长弓定要发射″，从其姓名就可感觉其人的倔强，果决与强硬，打起仗来，其可能为气焰难当的鹰派，而一旦遭屈受压，又有可能变为铤而走险的弑君者。”<br />
<br />
张定发果然成了铤而走险的弑君者。<br />
<br />
黄海刺胡事件，也似乎应验了《推背图》第四十六象那句“东边门里伏金剑”，因为青岛地处胶东半岛东部，青岛海港算是 “东边门里”，谋刺胡而未中可以用“伏金剑”来概括，伏而未中，只得继续“伏下去。“东边门里伏金剑”，后面紧跟一句“勇士后门入帝宫”，这像是在预测胡锦涛的命运。<br />
<br />
为什么这么说呢？许多善良的人士至今还在呼吁胡锦涛“退出中共，另立新党”，但促成胡锦涛这样做的可能性几乎为零，先别说胡锦涛没有这样的思想和觉悟，即使他有这样的思想和觉悟，他也没有这样做的实力，因为他处于今天巨大而且凶残中共权贵既得利益集团的包围之中，一旦 “背叛”，将立即被身边的群狼撕咬的得粉碎；胡锦涛唯一的安全出路是辞去中共国最高领导职务，但是由于巨大的权力和既得利益的腐蚀和诱惑，他这样做的可能性微乎其微，诚可谓权迷心窍，“利令智昏”也，因此，胡锦涛最有可能成为<br />
<br />
“勇士后门入帝宫”的被动主角。<br />
<br />
至于“勇士后门入帝宫”是一场什么样的变故？起事者会是谁？会在何时何地起事？天意难测，且天机不可泄露，我不敢妄作论断。<br />
<br />
