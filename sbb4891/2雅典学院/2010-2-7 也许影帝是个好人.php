胡锦涛2006年5月在北海视察险被暗杀，江胡权斗升级。胡撤换了北京卫戍区司令、政委，引发外界猜测北京高层将有异动。在高层分裂的情况下，有两个重要部门的掌控成为生死存亡的关键，一个是中央警卫局，另一个是北京卫戍区。胡锦涛正将江泽民嫡系调离北京，确保北京。<br />
<br />
《新纪元周刊》张海山报道， 最近北京卫戍区司令、政委双双换人，司令邱金凯中将刚上任不到一年就被撤下，而撤下的政委董吉顺少将更是在北京卫戍区根子非常深，曾任卫戍区政治部主任、副政委、政委。<br />
<br />
北京卫戍区换由第三十八集团军军长李少军少将和第二十七集团军政委刘福连少将分别接任司令、政委。<br />
<br />
胡锦涛今年5月在北海视察暗杀未遂，江胡权斗进入血战。在此背景之下，胡撤换北京卫戍区司令、政委引发外界猜测北京高层将有异动。北京是中共中央所在地，在高层分裂的情况下，有两个重要部门的掌控成为生死存亡的关键，一个是中央警卫局，另一个是北京卫戍区。胡锦涛死守北京。<br />
<br />
中央警卫局和北京卫戍区换血<br />
<br />
二OO六年一月，原北京军区参谋长邱金凯少将升任北京军区副司令员兼北京卫戍区司令员，并晋升中将，他并兼任中共北京市委常委。但还不到一年，现在却丧失了北京卫戍区司令的实权职位；而政委董吉顺是在卫戍区一级一级升上来的，哪一级都有“关系户”，现在却调任总政治部直属工作部当没有实权的政委。而李少军和刘福连都没有在地方军区工作的经验，年龄优势也仅仅小三岁。却从集团军调到如此重要的位置上。<br />
<br />
当年粉碎“四人帮”一举成功，北京卫戍区政委吴德与中央警卫团团长汪东兴协同华国锋，起到了至关重要的作用。警卫团负责直接抓捕行动，而北京卫戍区则负责对人民日报社、新华社、广播电台、中央机关以及重点部门的控制，并在中南海内如果出现了意料不到的问题，卫戍区负责部队支援，保证北京不乱，从而稳定全国局势。<br />
<br />
去年四月十八日胡锦涛访美之前，中共警卫局从班、排、连、营到师级的领导已全盘换过，现在从网上搜索中央警卫局仍然是局长由喜贵上将卦名，实际已重新成立，上将由喜贵如今只负责江泽民一个人的警卫工作，而中央警卫局的日常工作由胡锦涛卫士长副局长孙之功少将全盘主持。<br />
<br />
江在北京亲信纷纷被调到外地<br />
<br />
中央警卫局大换血没有多久，江泽民在北京的亲信纷纷被调到外地去，而且不少都复转回家了。随后胡温在对上海市和广东省江泽民势力中心重点清洗，江家帮借机在北京放风说胡锦涛一旦整完那两个地方，就该整北京了，挨着个儿整，谁也跑不掉。于是，整个北京官场人心惶惶，更有人在诱导方向，说只要把胡锦涛除了，温家宝也翻不过大天去。但是在北京江势力没有直接动手的条件。<br />
<br />
北京卫戍区一直是胡锦涛比较忽视的地方，由于亲身体验江泽民和曾庆红这些年的利用中央警卫局对他的监控，所以胡对中央警卫局非常重视。<br />
<br />
胡锦涛去年5月在北海视察遭两艘军舰炮击事件经香港媒体披露后，虽未得到中共官方资讯证实，但随后发生的以反腐为名的清洗，已经使事件明朗化。<br />
<br />
消息来源称，此次江又筹画利用北京卫戍区反扑。结果一个卫戍区官员的老婆拉家常时说走了嘴，遂被举报。此次胡锦涛命令不动声色，等拿到证据后才动了手。<br />
<br />
中共内部权斗已经到了白热化程度，消息来源称，北海暗杀未遂事件后，现在青岛政府大大小小的头目几乎都换了个遍，官员都是从 济南调过去担任的。目前所有和张定发、杜世成稍微熟悉认识的人都被调查 。<br />
<br />
山东杜世成免职带下一片<br />
<br />
12月24日新华社报导，中共山东省委副书记、青岛市委书记杜世成因违反党纪遭到免职。<br />
<br />
据报导，杜世成涉嫌在国有土地转让批租中黑箱操作，多次收受房地产商贿赂，为亲属谋利。杜世成自己在崂山区拥有价值千万元的豪宅，并且长期包养情妇。<br />
<br />
去年5月江泽民游山东期间，杜世成为了讨好江泽民，把从青岛到崂山沿途 “八荣八耻”的标语，全部换成“三个代表”。<br />
<br />
内部消息称，杜世成下台的真正原因是杜与张定发5月袭击胡锦涛事件有关。青岛方面知情人透露，现在青岛政府大大小小的头目几乎都换了个遍，官员都是从济南调过去担任的。目前所有和张定发、杜世成稍微熟悉认识的人都被调查 。<br />
<br />
张定发去世极低调<br />
<br />
海军前司令、中共中央军委委员张定发日前因病在北京逝世，消息不是按惯例由官方新华社或解放军报发布，而仅由海军自己的报纸低调披露，已经使外界注意到其中的极不寻常。<br />
<br />
“人民海军报”12月17日在头版刊出简讯指，“中央军委委员、海军原司令张定发同志，因病于12月14日在北京逝世，享年63岁”。消息连遗照也未配发， 只有简单履历。<br />
<br />
张定发，上海吴淞人，1964年毕业于海军潜水艇学校，青岛是张定发的发迹地，他历任北海舰队参谋长助理、青岛基地参谋长、北海舰队参谋长、济南军区副司令员兼海军北海舰队司令员、解放军海军副司令员。<br />
<br />
胡锦涛遇袭与高调反腐<br />
<br />
据12月1日新唐人电视台报导，今年五一黄金周，江泽民到泰山、潍坊等地“旅游”期间，胡锦涛也在这个时候秘密到达青岛，视察北海舰队的一场军事演习。<br />
<br />
当胡乘坐北海舰队最先进的一艘导弹驱逐舰到黄海时，突然有两艘军舰同时向胡乘坐的导弹驱逐舰开火，当场打死驱逐舰上5名海军士兵，导弹驱逐舰立即载胡疾速驰离舰队演习海域，直到安全海域，胡立即换乘舰上的直升飞机飞回青岛基地。胡未作停留，也未回北京，直飞云南。<br />
<br />
有消息称，被拘捕的军官承认，使用这么精良的舰艇决不可能失手，但是左右夹击居然让胡锦涛死里逃生，这无法解释。他们说，当时哪怕考虑有百分之一失手的可能性，也决不敢迈出这一步，也没有胆量拿自己小命开玩笑。<br />
<br />
新华社曾报导胡锦涛的行程，11日至15日 胡在云南考察。<br />
<br />
胡锦涛16日回北京之后，采取的第一个行动是保外就医陈希同。旨在释放反击江泽民上海帮的信号。胡第二个行动就 是以反腐败的名义搞倒海军副司令王守业，获取了王守业与其后台江泽民秘书、现军委办公厅主任贾廷安的共同罪证，逼迫江泽民不能再插手军内事务。<br />
<br />
《未来中国论坛》发言人伍凡认为，胡锦涛遇到了军人袭击，这可是大事情。这说明了中共内部斗争已经到了一个极为严重的境地。军人也参与其中了。大家不用问，能采取这个行动的人只有是江泽民派系的人才能干。背后一定有江泽民的影子。<br />
<br />
北京不能乱<br />
<br />
当年在粉碎“四人帮”的问题上，华国锋嘱咐北京卫戍区政委吴德说：首都不能乱，首都一乱，全国就有可能发生大问题。稳定首都的问题，由你负全责。<br />
<br />
吴德回忆说，中央警卫团团长江东兴是出了大力的。毫无疑问，他要不配合，那中南海里可能要见血。吴德回忆，总理华国锋、叶剑英元帅找汪东兴秘密谈过几次，具体研究了解决“四人帮”的办法。当时成立了两个小班子，一个准备有关文件，由李鑫负责；另一个负责对“四人帮”实施隔离审查，这个班子的人员是由汪东兴亲自从中办和中央警卫团挑选并个别谈话后组织起来的，大概有五十多个值得信任的人，组成了几个行动小组，一个组负责抓一个人。<br />
<br />
现在基本上胡温整治官场也走了这条路，在江泽民的势力地盘上海和广东开刀，把江基本上圈禁在北京，把专门病得要死的黄菊送回上海，北京除了被上海牵连的个别人外，北京官场基本不动、保持稳定。<br />
<br />
胡温中共高层会不会重演当年粉碎“四人帮” 的一幕，彻底铲除江的隐患，是海内外密切观察的焦点。<br />
<br />
前海军司令员张定发逝世 军方低调登讣告耐人寻味<br />
据知情人士透露：中央军委委员、海军司令员张定发，因患晚期肺癌，已住院治疗。 今年一月下旬，张定发到青岛北海舰队、烟台海军航空兵基地视察，慰问驻军，在出席会议时突然昏迷不醒，有关当局派专机将其送返北京治疗。四月初，中央军委宣布，由副总参谋长吴胜利任海军司令员，参加中央军委工作。 <br />
<br />
既然上面把张定发（刺胡直接下命令者）点到了，我就把“黄海事件”聊聊。<br />
事情发生在2006年5月，离禁止讨论政治话题十七大还有大约一年，禁止讨论政治话题高层你死我活的内斗进入短兵相接和肉搏阶段。<br />
胡锦涛2006年5月在黄海视察险遭军事暗杀事件，促成这次至今仍是绝密的“江湖”生死决战。劫后余生的胡锦涛大规模清洗J家军和上海帮，近来，党中央的整肃风暴直指JZM之子JMH。<br />
这说明陈良宇与周正毅同时案发非偶然。网民上网，即便最普通的搜索都可搜索到有关JZM长子JMH的负面消息及JZM与歌星宋祖英的绯闻，并出现“JMH涉案”、“JMH被抓”、“JMH内幕”、“JMH与陈良宇”、“JMH贪污”、“JMH与JZM”、“宋祖英、JZM绯闻”等搜索词。<br />
这些负面消息出现最频繁的是JMH涉周正毅案。周正毅日前再次被抓，绝对与打击JMH有关。JMH会不会成为党中央下一个清洗目标，JZM的结局如何，“江家帮”是否会落得当年“四人帮”的下场，很快就有答案。<br />
<br />
原因是事情的性质非常恶劣。<br />
<br />
胡锦涛上台后，对JZM上海帮的贪腐淫乱、为非作歹及镇压迫害采取姑息迁就态度，他以为这样就可保住手中的权力。但黄海暗杀事件成为转折点，险些丧命的胡锦涛意识到：这场“江湖”恶斗是实打实的你死我活、没有妥协余地。他的对手为了夺权和免受清算，不惜发动军事暗杀，要置他于死地。<br />
<br />
所有这些已被查实。<br />
<br />
事情发生在2006年“五一”期间，JZM一行在山东“游山玩水”，胡锦涛赴青岛视察海军演习。当总书记乘最先进的导弹驱逐舰行驶于黄海海面时，其座舰突然同时遭两艘海军驱逐舰机关炮扫射，身边的五名海军士兵被当即打死……<br />
<br />
在这种情况下，惊魂未定的胡锦涛即刻换乘舰上直升飞机飞云南，一个星期后，确定一切安排就绪才飞回北京。<br />
<br />
调查结果：黄海刺杀胡锦涛是JZM和ZQH共同谋划。ZQH出于帝王梦，要除掉胡锦涛取而代之。JZM则是出于ZQH掌权他才放心，ZQH不会追究其的贪腐和罪恶，同时会提携他的儿子JMH和JMK。<br />
