													<h2>曹长青：罗宾汉是最不道德的文学形象</h2>
						
						
																											<div id="postmessage_673355" class="t_msgfont">被称为史诗般巨作的好莱坞新片《罗宾汉》，最近在美国、欧洲和亚洲等国家上映，并成为法国坎城电影节的开幕影片，备受瞩目。该片由好莱坞多位大牌明星联合主演，男主角是曾在《角斗士》中扮演英雄的罗素&#8226;克劳，女主角是著名影星凯特&#8226;布兰切特。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
<br />
&nbsp; &nbsp; 《罗宾汉》这部大片，是根据英国传说中的绿林好汉罗宾汉的故事拍摄的。这个题材并不新鲜，人类自有电影以来，罗宾汉的故事已被拍过好多次：最早的一部，是中国辛亥革命前三年的1908年拍的，然后1922年又拍了第二部，片名就叫《罗宾汉》，被视为默片时代的动作片代表，以场面辉煌、耗资巨大著称。1938年好莱坞推出的《罗宾汉历险记》，成为罗宾汉电影的经典，有评论说，该片对罗宾汉的造型和诠释，其经典地位，至今都无法撼动。到了1991年，曾导演和主演《与狼共舞》的凯文．科斯纳，又拍了部《侠盗王子罗宾汉》。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 对这位传说中的劫富济贫的绿林好汉，电影业屡拍不断，观众也屡见不烦，可见人类对劫富济贫、要求财富平等的偏爱。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 她的书影响力仅次于《圣经》<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 但是，美国著名的哲学家、畅销书作家安&#8226;兰德（Ayn Rand），却对罗宾汉的这个文学形象提出批评，认为劫富济贫的罗宾汉，是人类文学作品中被热烈讴歌的英雄中“最不道德的形象”。用了最不道德这个评语，可见这位美国哲学家和畅销书作家，对罗宾汉这个形象的反感甚至痛恨。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 安&#8226;兰德是客观主义哲学的创始人，在美国有极为特殊的重要地位。在1998年美国兰登出版社做的《二十世纪百部最佳英文小说》评选中，在“读者投票评选榜”上，安兰德的代表作小说《阿特拉斯耸耸肩》获第一名，另一重要小说《源泉》获第二名。她的另两部小说（《颂歌》、《我们活着的人》）分别排第七、第八位。而她一共就出版过四部小说。20世纪出产的英文小说数量，远超过以往任何世纪，但安兰德一个人，就囊括了前八名中的四名。她的小说全都不是娱乐作品，而是深刻的哲学探讨。能受美国大众如此推崇，绝不是偶然，而是有其深邃的历史和现实原因！<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 1991年，美国国会图书馆和全美最大图书俱乐部做了一次读者调查，在被问到“最影响你一生想法”的书时，安兰德的《阿特拉斯耸耸肩》仅次于《圣经》，排第二位。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 抢银行救济穷人有什么不对？<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 这样一位重要的哲学家和畅销书作家，为什么如此反感罗宾汉这个深入人心的文学形象？罗宾汉劫富济贫，有什么不好？安兰德认为，恰恰是这个“劫富济贫”有问题，因为它是强行抢夺别人的劳动成果，虽然目的是为了帮助穷人。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 安兰德认为，用这种方式推行平等，均贫富，是社会主义和共产主义的思想根源之一。共产党就是以人民的名义、人民的利益等等，抢夺有产者的资产。当年毛泽东的打土豪分田地，就是大规模的罗宾汉化，也是用武力方式，抢夺富人资产，然后以所谓平等的名义分给穷人。有人会说，那分给穷人有什么不好？且不谈这个目的本身的问题，即使这个目的没有错，但手段明显错了，犯了“为了目的不择手段”的错误。因为如果肯定“为了目的可以不择手段”，那么今天人类的恐怖分子活动，也就可以成立了，他们也说自己的目的是高尚的。看一件事情，关键不是它的目的怎样，是不是高尚，而是看它的手段，是不是恶劣。只要手段恶劣，就绝不会有真正高尚的目的。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 据《华尔街日报》2006年5月的报道，在德国，就有一些人热衷做现代罗宾汉，居然在光天化日之下，在汉堡市抢劫了一家高级食品店，抢走的香槟酒、牛肉、高档巧克力等，价值1500欧元。但他们不是一般抢匪，而是要把这些食物分给穷人。他们的口号是“汉堡免费”（Hamburg for free）。而在这之前不久，一伙主张社会主义的左派，抢劫了汉堡一家餐馆，也是要把食物分给穷人。《华尔街日报》说，这些“超级英雄要跟西方最大的罪犯――资本主义――进行战斗，实现均贫富。”<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 安兰德对罗宾汉这个文学形象的批评，以及她的核心思想，是强调人的自由，个人的权利，这是人类最重要的价值，而对这个价值的践踏是罪恶和邪恶。在人的权利中，最关键的财产权，不可有任何理由剥夺，不管你是均贫富，还是济贫，都不可以。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; “没有财产权就不可能有人权”<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 兰德曾经在批判社会主义的经典文章《建造纪念碑的人们》（The Monument Builders）中指出，“当你考虑社会主义的时候，你要清楚它的本质，不可自欺欺人。记住，没有‘人权’和‘财产权’的对立。没有财产权就不可能有人权。既然物质商品是由个人的思维和劳作所生产的，也是维持他们的生命所必需的，如果生产者不能拥有他们劳作的成果，他就不能拥有他的生命。拒绝财产权意味着把人变成国家财产。任何人宣称拥有‘重新分配他人财富’的‘权利’，就是宣称他有把人变成奴隶的‘权利’”。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 今天西方的资本主义制度，就是建立在保障人的财产权的基础之上的；并有“私有财产神圣不可侵犯”这种文化上的认知，更有法律层面的保护。这是西方文明的核心价值之一。这也是为什么今天美国民间抗议、反对奥巴马倾向社会主义政策的茶党（Tea Party）运动风起云涌的原因之一。美国人拒绝政府用高税收这种强制手段（不交税要坐牢）来抢夺勤劳致富者的私有财产，拒绝以平等的名义，进行社会财富二次分配，以国家力量走向“罗宾汉化”。因为共产主义和一切极权社会，都是用为“人民”“多数”“集体利益”的理由，剥夺个人的权利；而一切权利的剥夺，都是从剥夺财产权（共产）开始。安兰德否定罗宾汉这个文学形象，是否定这种剥夺别人自由和财产、由集体主义走向极权主义的思维和道路。</div>

							
							
							
							
