																											<div id="postmessage_482046" class="t_msgfont">谭家有女初长成<br />
艾晓明<br />
<br />
图一：判决五年 律师出来接受电话采访 老羊拍<br />
&lt;img src=&quot;http://imgur.com/3ugpg.jpg&quot; alt=&quot;Hosted by imgur.com&quot; /&gt;<br />
<br />
谭作人一审判决，作人被关监五年。尘埃落定，我也要打道回府。临行前，我跟王庆华说，我想见见孩子们，请她们吃顿饭。<br />
<br />
庆华说，畅畅晚上还没下班。小蒙不见你。<br />
<br />
我说这年头是不是见鬼了，一个背摄像机的人，社会地位落了好几十丈，青红帮喊打喊杀，求见个孩子都这么难。我说，你再打电话，我不是为了小蒙出镜，只是吃顿饭，过年了嘛。庆华打开手机，继而转述，小蒙说：哎呀，能不能不吃饭嘛。<br />
<br />
第一次见到小蒙，是2009年5&#8226;12周年祭前夕，作人已被拘留一个多月，这是我第一次去到谭作人的家。在此之前，我知道谭作人的父亲是川大历史系知名教授，后来我在网页上看到，其父谭英华先生，祖籍湖南，博古通今、学贯中西，是川大西方史学史研究的奠基者。而此前和作人聊天时，我只记得一件趣闻，谭先生的研究领域之一是民族关系，其《明代对藏关系考》手稿流落在北京的旧货市场潘家园。如今再查谭英华，不翻墙得名目三条，翻墙得108条，多数网页显示无法打开。此中天机天知道。<br />
<br />
在川大校园一栋旧楼里，我进了作人的家――谭英华先生留下的三居室。为作人插刀的小白狗跑过来，狗背上少了一大片毛，针线刀疤历历在目。小蒙蜗居于一小间，埋头电脑游戏。作人那间黑灯瞎火，窗上挂了布帘子。然而，一盏小小的节能灯，也足以让我看清作人的清贫。<br />
<br />
这个清贫，怎么说呢？如果你知道当年的五七干校，作人那间书房，就跟五七干校差不多，一个破桌子，上面连接着书架，堆放着很多手稿，在每个架上，贴了几个标签“项目”、“策划”等。右手边是一个近乎歪倒的沙发，竖着的部分是猫狗鹰爪拳的练武之地，放平了就是作人的卧榻。这屋子的家具，老实说，全部扔到外面草地上，估计没有人捡。庆华在自己的那间卧室点亮了灯，房间里响着个老式的破电视。作人家的其他角落我就不一一介绍了，你能想到五七干校的简陋，那就再加个乱字。作人就是在这里写作思考，表达着他对四川成都的大爱大恸。我建议成都公检法以及所有公务员考核增加一个环节，参观颠覆国家罪犯谭作人居室。在这里，你立即就能找到思想良心犯和窃国大盗的根本距离。<br />
<br />
图二：作人的书架 艾晓明拍<br />
&lt;img src=&quot;http://imgur.com/fMzxQ.jpg&quot; alt=&quot;Hosted by imgur.com&quot; /&gt;<br />
<br />
我想再见小蒙，因为在纪录片《公民调查》中，使用了对小蒙的采访；我想要谢谢她，还想告诉她观众的认同。我听说张思之大律师看过，为孩子对爸爸的理解而感动。但庆华说小蒙不喜欢，认为拍得太难看了。那是自然，拍谁的后脑勺也好不了。我在小蒙房间，原非执意采访，更不想迫使她面对镜头。一个不满16岁的孩子，忽然间爸爸被一大堆警察抓走，这个颠倒的世界，孩子如何解释？<br />
<br />
1969年，我在小蒙的年龄，我的父亲，也是学校里的现行反革命。那年的革命委员会，不知从哪里学来一招，在一次批判会上，把我叫到现场。我父亲坐在哪里低头不语，周围人一个接一个批判他的过去。其中有一个问题让我如坐针毡：那年你在国民党军队，一炮炸死了多少人？晚上回到家里，我鼓足了勇气走过去说：爸你就交代了吧。我爸长叹：该交代的都交代了，我不能瞎说，瞎说要负责任的啊。<br />
<br />
2009年，四十年过去，九零后的谭小蒙，在电脑上敲击游戏，屏幕上，一群骠骑兵策马奔突，那是谭作人被捕时的情形。她目睹警察进门、抄家、把作人带走。在电脑前她给妈妈报信，庆华说：你把门打开（可以听见那边抄家的声音），告诉他们，你们要把我爸带走，一定要告诉我妈。<br />
<br />
九零后的谭小蒙，在爸爸身边生活不到16年。从1993年到2009年，这些年，谭作人做过政府策划、当过成都市“十佳市民”，也买过破车挣过钱。熟悉谭作人的都知道，他要去发财，绝对不是没那个本事，只是心不在焉。正如王庆华所说：二十多年了，谭作人对国家的大忠远远多于对小家的小孝。他数次哀叹：太对不起家人了！我劝慰他：自古忠孝难两全，大忠即为大孝。<br />
<br />
图三：作人在北川中学废墟前 艾晓明拍<br />
&lt;img src=&quot;http://imgur.com/zCEmP.jpg&quot; alt=&quot;Hosted by imgur.com&quot; /&gt;<br />
<br />
在王庆华的文章里，我看到小蒙给爸爸的信：<br />
<br />
爸：五月十五快到了，提前祝你生日快乐，并给你讲一个儿时听过的童话故事。在一个国家，有一个国王和三个公主。一天国王问三个公主：'你们有多爱我？'大公主说：'我象爱蜜糖一样爱您。'二公主说：'我象爱花朵一样爱您。'国王听后十分满意。问到三公主时，三公主说：'我象爱盐一样爱您。'国王一听勃然大怒，立刻下令将三公主关进牢房。故事讲到这里，我不禁一阵悲哀。不知三公主被她最爱的父亲关进牢里，心中是怎样的凄凉？明明如此爱着父亲，只是爱的方式不同，不象其他公主那样甜言蜜语，就被下了大牢。三公主何罪之有？其实这个故事有个相当好的结局。一个邻国的国王听说了此事，便向三公主的父亲发出邀请参加一个盛典。<br />
<br />
席间很多佳肴。一尝，味如嚼蜡。这时有人告诉他：菜里都没有放盐！国王恍然大悟，原来渺小的盐不甜不香，却是最必不可少的一部分。国王马上回宫下令释放三公主。从此以后，三公主得到国王最多的宠爱。这是有名的童话《盐之公主》。故事有个好结局，但现实生活中的结局令人叹息。国王能良心发现吗？悲哀！你的家人和朋友、猫猫狗狗、花花草草依旧思念你，愿大家的关心能如清风，在炎夏给你带来一丝清凉。你的女儿。<br />
<br />
记得在王康先生的文章里有曰：四川是中国的盐；小蒙的信让我想到：作人是四川的盐。而在五十五岁生日的第二天，作人在温江看守所写下这样的回复：<br />
<br />
我的女儿：此间规则为逢十发信，但我等不及了。面对你送上的生日礼物----一封充满大爱的家信和100元钱，我失眠了。我无数次披衣而起，仰望铁条分割的夜，想念同城异地的你！此刻的我，再一次明白了一个道理：有的'近'，其实很远；有的'远'，实在太近！我们分别已经50天整，但我却时时在在地感觉到，我每一天都能看见你......一代人有一代人的价值，一个人有一个人的命运。我们这一代人，是集体主义的产物。这是宿命也是一种使命----因为集体。有集体的或者公共的价值需要传递下去。有人选择了这件工作，使命就可能成为史命----一种历史的创造和命定。这并不等于说个体的价值不重要。在二元乃至多元的文明中，集体和个体各自存在，相安无事，共生共存。在我为人人向人人为我的转型期中，需要两个以上的声音。当三公主的盐成为稀缺资源的时候，我们这一代人，应该有人惭愧。因此，三公主选择了看见和说出，他不会感到委屈，请你放心。心，就是为了给予，伴随着一滴眼泪（泰戈尔）。这支歌曲，是大家的。这滴眼泪，是自己的----或者说，是自己和自己的家人共有的。我可以毫无愧色地说：我对得起这个社会。但我对不起你们----我最亲最爱的人。你们，就是我的眼泪。......想念你的爸爸。<br />
<br />
图四：浦志强律师电脑桌面上的谭作人 艾晓明拍<br />
&lt;img src=&quot;http://imgur.com/6vdJG.jpg&quot; alt=&quot;Hosted by imgur.com&quot; /&gt;<br />
<br />
月明星稀，我们在冷飕飕的校园里漫走，庆华说，2月9日清晨她从梦中惊醒，梦见谭作人回家了，直接开门而进。她惊呼，作人你怎么不拿钥匙？醒来就想糟了，梦都是反的。<br />
<br />
我就是这样，再一次来到作人的家，我说，我不要做采访，也不需要拍摄，我只是想看看孩子。我理解庆华所说，16岁的孩子，最不喜和大人相处，躲还躲不赢，也不要吃饭。我16岁时，比她还要烦大人。但我们依然在寒风中走了很远，走进这栋小楼，这个让作人魂牵梦绕的屋子。小蒙出来叫阿姨好，我骂庆华造谣可耻，谁说小蒙不见我。<br />
<br />
和我上次来时相比，屋里点上了明亮的节能灯，堂堂地照耀着聊聊可数的旧家具。书架上的稿纸，一摞摞都已褪色。也真是奇了怪了，这么多项目策划警察都不要，在人家电脑里挖了封电子邮件，判他个五年监禁。更何况，电脑还是从房间里偷出去的！<br />
<br />
庆华拿东西给我看，打开的衣柜，所有的衣物折叠得整整齐齐；而且，庆华强调说：叠了两遍。第一遍是作人走后，全部洗净叠好。第二遍是楼上漏水，全部重洗、重叠。就在一天之前，不是有风传作人判二缓三吗？还有人准备放鞭炮呢。<br />
<br />
图五：揪住他不放 艾晓明拍<br />
&lt;img src=&quot;http://imgur.com/Xj7D9.jpg&quot; alt=&quot;Hosted by imgur.com&quot; /&gt;<br />
<br />
庆华给我看的东西，我再次建议将来的中国历史博物馆予以珍藏（上一次我建议收藏的是映秀小学生马冯艳的作文）。庆华说，六十大庆，有传说上千人将在天府广场打酱油，戴口罩，穿件作人衣。这个谣言把警察害苦了，满城寻找白衣裳。我眼前出现了电脑游戏中的场景，金戈铁马，气吞万里如虎；其实那日天府广场岁月静好，也没有车逆行。倒是一位叫大爷的人绞尽脑汁，找出来十件衣裳，成全了警察壮举。<br />
<br />
这一天四川大学校园里花红草绿，欢乐祥和。谁也没有想到的是，豆蔻年华的谭小蒙，自己决定要打一趟酱油了。小蒙把老妈打包裹紧的衣裳拿出来，那是从一个爷们身上扒下来的，后面签着他的恶名；歪歪倒倒的，看来文化不高，连个名字也不会写。反过来，衣裳正面是一张单纯的脸，一双干净的眼睛凝视着路人。<br />
<br />
图六：证人艾未未签名 艾晓明拍<br />
 src=&quot;http://imgur.com/dIHql.jpg&quot; alt=&quot;Hosted by imgur.com&quot; /&gt;<br />
<br />
图七：小蒙的话 艾晓明拍<br />
&lt;img src=&quot;http://imgur.com/Ju9qq.jpg&quot; alt=&quot;Hosted by imgur.com&quot; /&gt;<br />
<br />
小蒙在这衣裳上面加上一行字：中国的心，心是画的，里面还有个笑脸。又在右侧竖着写上：他有何罪，后面画了三个问号。在右边最上角，还画了一个小小的十字。小蒙接着写下四个掷地有声的字：还他自由。画成，把这件二百多斤高头大马穿过的衫子套过了头，小蒙施施然、翩翩婷婷，在校园里招摇过市。<br />
<br />
总人口一千多万的成都，就这样出现了传言中的白衣示威者。王庆华接到片警好几个电话，只是不明白何罪之有，她穿的衣裳上是她爸爸，侵犯了谁的肖像权吗？<br />
<br />
在那篇写于8月6日谭作人一审开庭前的文章里，庆华说：直至今日我仍然坚信，他那么爱的国家无论如何不会判他&quot;颠覆&quot;之罪！庆华的信仰落了空，连小蒙的小朋友也不如。四十年前，我爸爸当反革命时，我经历了实实在在的唾弃；而小蒙的朋友们却在喊：谭爸爸，加油！<br />
<br />
开庭时，小蒙被拦截在庭外，这次判决，小蒙依然没有见到谭作人。当警察排成人墙推走律师和采访者时，小蒙在母亲身边，高高举起了手机，她拍下了这历史性的一幕。一位警官婉言相劝：妹妹，不要拍。小蒙说，我要拍，我就要拍。庆华说，你们判她爸爸坐牢五年，她为什么不能拍。律师说，你们是刑警、交警还是法警，这里没有治安案件、没有交通事故也不属于法庭。庆华还说：脱了衣服你们跟我们是一样的。一位警官居然应道：我们是一样的。<br />
<br />
不，不一样。法庭内外、大墙之下，谭作人和亲人咫尺天涯。他能见到的只有律师，而律师再也无法录像，作人也不能托他们带封家信。他的手现在被铐在会见室的铁椅子上了，律师给我们学他偏着脑袋凑到手上抽烟的模样，嘴里川腔说沟日滴判得太重了。<br />
<br />
五年后，作人是六十岁，小蒙是二十一岁的少女。这是怎样的五年呢？也许应该用土匪冉家的狗（它叫狄更斯）来说明：这是最好的时代，这是最坏的时代，这是智慧的时代，这是愚蠢的时代；这是信仰的时期，这是怀疑的时期；这是光明的季节，这是黑暗的季节；这是希望之春，这是失望之冬；人们面前有着各样事物，人们面前一无所有；人们正在直登天堂；人们正在直下地狱。<br />
<br />
庆华将带孩子去外婆家过年，作人在铁窗内等待或者不等待二审宣判。斗转星移，世界在九零后、零零后手中。我们注定灰飞烟灭，他们势必如日中天；窃听或风暴，遭遇青春之猛烈，可堪匹敌？我为五年之始的谭小蒙留影，等待作人出狱。<br />
