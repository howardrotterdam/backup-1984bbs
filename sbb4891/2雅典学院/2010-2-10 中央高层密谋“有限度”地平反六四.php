													<h2>中央高层密谋“有限度”地平反六四</h2>
						
						
																											<div id="postmessage_474829" class="t_msgfont">云飞杨：中央高层密谋“有限度”地平反六四/博讯独家<br />
(博讯北京时间2010年2月09日 首发 - 支持此文作者/记者)<br />
博讯记者：已经讨论到这个层级了？为什么一直密不透风？中共会突然来这样一个一百八十度的大转弯？<br />
云飞扬：具体的我也不清楚，但我知道，这个工作其实已经准备很久了，早在两年前，国安部已经负责收集世界各地对平反六四的方式方法建议以及看法，其中还接触了一些海外几位比较温和的民运人士。他们其实应该感觉到了。当然，这个消息最好能够密不透风，否则也许会有变数。（boxun.com)<br />
<br />
博讯记者：你说的变数是指什么？<br />
云飞扬：虽然目前手上真正沾了六四鲜血的只有李鹏一位，但他已经离不开呼吸器了，不是大问题，但是，整整20年在党内对六四的丑化，也毒化了一大批党员干部，如何让他们的思想转弯也是一个很大的任务。如果抵触情绪太大，胡温作出最后决定之前，都有可能生变。<br />
<br />
博讯记者：这样的事情总应该精心考虑，也不是说变就变的，我总感觉到有点玄。<br />
云飞扬：也没有什么玄不玄的，其实，早在上个世纪九十年代末，就在邓小平刚刚去世不久，江泽民就曾经考虑用平反六四来摆脱邓小平的阴霾，树立自己的威信，可惜，后来却不了了之，有人说是他无法摆平军队，还有几个大佬没有死绝；有人说是因为法轮功事件打破了他的计划，法轮功事件后，江泽民手上沾了血，已经无法有更开明的动作，担心自己被报复；还有人说的更有意思，说是他担心胡锦涛上台后会找他算账。因为平反六四是在邓小平死后，江泽民公然和邓划清界限的行为，可是，当时他只有三年期限就要下台，而他下台后就得把权力交给邓小平隔代指定的胡锦涛，所以，他不得不放弃平反六四。(boxun.com)<br />
<br />
博讯记者：云大侠，你让我目瞪口呆了，按照你的说法，邓小平身后的两位最高领导人竟然都在下台前三年就有平反六四的念头？你这是不是太一厢情愿了？<br />
云飞扬：我告诉过你，关于这件事，不是我是否情愿，而是我得到的一些不全面的信息这样告诉我，和我的猜测无关。江泽民和胡锦涛都想在自己下台前平反六四，有什么不好理解的？你以为江泽民和胡锦涛看不清历史？他们都清楚，不管是谁，只要能够平反六四，他的历史地位就会大不一样，所以平反六四对于他们来说，并不是一件无私的“高尚事件”，恰恰是他们想青史留名。当然，他们两人大概打的如意算盘都是一样的，既平反六四，又要确保中共政权。<br />
<br />
博讯记者：云大侠，按照你的说法，那胡锦涛当今平反六四的最大阻力是什么？平反六四对他有什么好处？<br />
云飞扬：对他的好处就太大了，六四过去20年了，手上沾血的几乎没有几个了，但民族心灵的伤痕却仍然在流血，胡锦涛如果能够在这个时候――我解释一下，为什么是这个时候，也就是在自己离开最高权力三年时间的时候？因为这个时候是他们掌握了权力的顶峰时刻――为六四平反，而他又能够摆平党内外的关系，那么，中国肯定会进入一个真正“和谐”的时代，这正是胡锦涛这一届领导人追求的。再说，大家也看到，胡温这一届政府上来后，做了很多实事，为民众做了不少好事，可是，他们却在意识形态上鲜有创树，往往给人一个左的印象。而且这也让他们有吃力不讨好的感觉。我相信，胡温两人早就感觉到这种让人气馁的局面，而如果他们能够在任内有限度的平反六四，又是的局面不失控，胡锦涛在历史上的地位很可能超过其它中共领导人。<br />
<br />
博讯记者：不要好意思，刚才还问了一个问题，他遇到的最大阻力是什么？<br />
云飞扬：主要阻力当然来自党内既得利益集团，他们担心平反六四会不会失控，中共政权是否能够保住。对于胡锦涛，这个问题当然同样重要，胡锦涛是否有想当叶利钦的想法我不清楚，但他绝对不想当戈尔巴乔夫，这是肯定的，谁都不想当戈尔巴乔夫，而叶利钦，则是民主俄国第一位总统。我这说的是最主要阻力，当然还有一些阻力，例如，正如江泽民当时想平反六四，却忌惮邓小平隔代指定的接班人胡锦涛一样，胡锦涛如果真要下决心平反六四，他肯定也会担心和害怕江泽民隔代指定的接班人。<br />
<br />
博讯记者：历史太相似了――<br />
云飞扬：但我这次倒真不想历史那么相似，否则对于我们这些期盼平反六四的人来说，就是重蹈覆辙了。说实话，当初我写《008在行动》的时候，很多人说我是江泽民的人，其实，不管从哪个角度说，江泽民都是一个思想相对开明的中共最高领导人，可惜，他错过了历史，历史也错过了他，江泽民的历史地位其实是很尴尬的。当然，更让人感觉到尴尬的，很可能是错过了历史的他，有时竟然不想退出历史舞台。这悲剧是全中国人的，自从改革开放之后，中国的政权中一直存在两个权力中心――这一点和西方倒是很相似，只是人家的两个权力中心（国会与行政）是相互制衡，而中国的则是相互牵制，相互制衡的西方避免了任何一方作出错误决定；而互相牵制的中国的两个权力中心，则阻止了任何一方大胆走向改革开放和政治体制改革。<br />
<br />
博讯记者：我听你说的，觉得无论是江泽民还是胡锦涛，好像都挺伟大似的，这是不是和我们从这么多年的现实中观察到的有些出入？这是否和你是体制内的人有关？<br />
云飞扬：也许我说的都是找好的说，其实，平反六四还有很多因素的，除了前面说的历史感和伟人感，还有比较丑恶的一面。例如在权力斗争中，平反六四的一方，往往立即会得到广大的中国民众，特别是目前几乎占据了大学、媒体等领域的自由知识分子们的支持。而正如我前面所说，如果操作得当的话，平反六四又不会真正得罪某一个团体和族群。<br />
<br />
博讯记者：谢谢，希望你的看法――不，希望你观察和听来的消息不是空穴来风。<br />
云飞扬：我也这样期盼，而且每天都在祈祷。</div>

							
							
							
							
