													<h2>李天笑：南京大爆炸炸出半个黑幕</h2>
						
						
																											<div id="postmessage_773109" class="t_msgfont">7月28日上午，南京发生了史上罕见的大爆炸。随着巨响，南京地面剧烈摇晃并伴有巨大轰鸣声，上空蘑菇云烟柱腾空而起，超强的冲击波震碎了方圆3、4公里的房子玻璃。震动波、火光和浓烟在十几公里外都能感受到和看到。死伤人员严重烧伤、许多人被烧的震的衣衫全无、有的耳朵被震聋、建筑物内外一片狼籍、诸多医院爆满… …惊骇之下产生的心灵震撼，有目击者用“地震”和“核爆”来形容。大爆炸带来的损失及后续的危害，目前无法估量。 <br />
<br />
这起南京市区近60年来最大的爆炸事故，其惨状与384年前明代天启年间（1626年）王恭厂大爆炸有相似之处。当时在北京王恭厂，即工部制造、储存火药的仓库一带发生了惊天动地的大爆炸，“两万多居民非死即伤，断臂者、折足者、破头者无数，尸骸遍地，秽气熏天，一片狼藉，惨不忍睹。”“天启大爆炸”中最奇异的是“脱衣”现象：“所伤男妇俱赤体，寸丝不挂”（《天变邸抄》）。后人估计这是爆炸后的冲击波所至。无独有偶。在南京大爆炸中，许多人衣服也全部被烧尽或震掉，附近上百棵法国大梧桐也被震得光秃秃的，震到地上的树叶虽是绿色，但一触即碎。 <br />
<br />
<br />
中共当局应对大爆炸的方式与明代皇帝有根本差别。熹宗当时受到震撼，下罪己诏，大赦天下，祈天安民，发府库万两黄金赈灾，此事被载入明朝正史。但中共当局到目前为止，没有半点引咎自责的表示，非但不深入调查事故原因，反而封锁消息，淡化灾难，推卸责任，尽量掩盖已经被炸出的黑幕。 <br />
<br />
<br />
首先，中共当局极力掩盖死亡数字。南京官方曾给各家媒体派红包，要他们把把死亡控制在“10人以内”。南京市长曾给医院下达指令，所有重伤患者一定要撑过三天，三天以后死亡就是因伤死亡。中共官员阻止了一开始的直播，尽管直播的原意也是歌颂领导人抗灾，然后按统一口径报导。截至到8月4日，官方只承认有13人在事故中丧生，120多人住院治疗，包括14名重伤者。同时，超过4300户居民遭受财产损失。 <br />
<br />
<br />
许多目击者强烈质疑官方的数字，因为这个谎撒的实在太离奇。一名事发后第一时间进入现场的市民表示，他亲眼所见，一间房屋倒塌后，一家六口仅有一人生还，此外一家名为中华饭店16名员工仅有1人生还。也就是说，仅这两处已有20人丧生，而现场是繁华地带，有多少民居和商家？另外爆炸是在堵车时发生的，现场施工拆迁的也有不少人，死亡数字应该是官方提出的几十倍才对。有网透露，死亡人数为259。但他很快被警方抓捕，说他散发虚假消息。实际中共当局是利用对新闻的垄断权在发布真正的虚假消息。 <br />
<br />
<br />
其次，南京大爆炸实际至少是两次，但官方说只有一次。当时在栖霞区万寿百江液化气厂有一次爆炸，稍后一次在栖霞区原南京塑料四厂厂区，而且是持续爆炸，从西霞大道烧到晓庄，范围2公里。官方只报导了后一次，说施工挖断了丙烯管道造成丙烯泄漏，遇明火引发爆炸，对前一次绝口不提。 <br />
<br />
<br />
再次，官方称事故现场明火当天上午被扑灭。但根据龙虎网报导，实际上明火烧了近 20小时到第二天（29日）早晨5时23分才被扑灭。也就是说，爆炸后20小时内地下管道的丙烯可能仍呈流动状态，而非官方所称的已呈非流动状态。 <br />
<br />
<br />
最后，在查找事故原因时，急于推出替罪羊，丢卒保帅。中共当局很快刑拘了四名肇事者，分别为扬州鸿运基础设备开发有限公司(“鸿运基备”)负责人绍建军，绍的亲戚董来荣，董某妻弟方强锋以及原塑料四厂安全工程负责人蒋山尊。中共明显把责任推给底层的包工者。这些包工者不但违规转包，而且缺乏拆迁资格，中标靠的是行贿。 <br />
<br />
<br />
按住房和城乡建设部09年“危险性较大的分部份项工程安全管理办法”，施工总承包单位必须编制专项方案，其中应当包括，工程概况、编制依据、劳动力计划等，并要进行专家论证和有计算书及相关图纸。问题在于“鸿运基备”不但不具备这些技术条件，而且在扬州市工商局无备案，是个皮包公司。它只是借用其他有资质公司的名头拿项目。是谁得了好处纵容和批准了这些包工项目？一个外来的扬州非法公司为何轻易拿到南京的项目？现南京市市长来自扬州，并在扬州有“拆迁市长”之称。他与这些包工项目有何关系？这些都是另一半待揭的黑幕。 <br />
<br />
<br />
看到这里，很多人自然都会想到一个问题，中共当局为何要死捂另一半黑幕？第一，这直接关系到中共地方各级官员的乌纱帽。死亡10人以上就要定性为重大责任事故，地方政府须不但要在规定时间内上报并接受上级单位的问责，而且连续或者多次发生重大责任事故，或者发生特大责任事故的，主要领导要引咎辞职，甚至决定对责任人大处分或刑责力度。所以，中共官员说谎不仅是个人行为，还是体制性弊病。 <br />
<br />
<br />
第二，这直接关系到中共政权的总体形象和维稳。封锁消息、编造假象，推咎下属才能保住中共整体。压制事故新闻不只是南京地方官僚的腐败，总后台是中宣部和中共高层。28日晚央视《新闻联播》生动地说明了这一点。当晚19时央视《新闻联播》的头条是“西部开发十年成绩辉煌”的专题，而南京大爆炸被放在国内新闻的最末一条。当天《新闻联播》的重点是胡锦涛致电慰问“巴基斯坦客机坠毁152人遇难”。许多人会愤怒地对着电视画面质问胡锦涛：为何只慰问外国事故受难者，却不及时安慰本国的“南京大爆炸”受难者？ <br />
<br />
<br />
其实很简单：胡关心的不是中国人的命，而是中共的面子。于是在吹捧歌颂中共领导人救灾的造假片未剪好之前，宁可不报或只报外国人。因此在中国，LV不是奢侈品，真相才是奢侈品。</div>

							
							
							
							
