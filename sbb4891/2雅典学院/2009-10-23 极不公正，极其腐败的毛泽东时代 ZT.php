																											<div id="postmessage_297471" class="t_msgfont">极不公正，极其腐败的毛泽东时代<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp;&nbsp; &nbsp; 当今毛泽东热确实从一定程度上是源于人们对社会公正的呼唤和对当今腐败现象的反感。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 但是，那种对毛时代廉洁，公正的想像却是虚妄的。事实上，毛泽东时代恰恰是一个极不公正又极其腐败的时代。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 是的，那个时候干部办事不收钱，计划经济嘛，金钱的作用本来就有限，高级旅馆，软卧，飞机，那都是要一定级别的人才能住（坐）的，这是不是腐败？你看他安排子女，亲戚朋友的工作用不用自己的权力？这不是以权谋私？ <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 那个时代是一个金钱不那么有用，而权力万能的时代，一切物资都按照权力大小，职位高低来分配，生活中几乎所有必须品都要凭票供应，这是看上去那个时代的人似乎要廉洁一些的原因。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 其次的原因就是那个时代的信息，新闻比现在控制得要厉害得多，很多人对一些事情不知情而已。那个时代的全国人民知道北京的功臣们的子女上的是专门为他们办的中，小学吗？知道中南海特供给的几分钱一斤的大龙虾么？　国家拨款给中国农业方面最高的科研机构搞特供蔬菜，特供牛肉等等无偿供应给特供商店，再让高干的家属们以象征性的价格搬回家中，这是不是集体腐败？ <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 知道毛泽东有过多少女人么？当然拉，他是用不着化钱的。如果那时有互联网，如果那时有现在这般的相对的新闻言论自由的话，那么当全国人民知道中南海专门养个文工团，以革命的名义挑选不满18 岁的少女陪舞作乐的话，那BBS论坛上会是怎么个样子，是否会不如宝马杀人案热闹？清清白白的姑娘陪毛泽东前得先用消毒剂净手，这个和恶棍乌代有何区别？这和封建皇帝有何区别？<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;“花天酒地，吃山珍野味，穿凌罗绸缎，住宾馆别墅，整天嫖嫖泡泡”，好像那个时代中共的干部就不干这个似的。康生花几角钱就将珍贵文物据为己有，这不是贪污？无数女知青为了回城，为了一个红印章而被迫献出自己的贞操，那不叫有腐败？平民百姓要买自行车，名牌香烟等紧俏商品也要开后门，这叫公平？就是所谓领导阶级的内部也有“一等公民方向盘，二等公民售货员。。。” 的说法，难道这就是社会主义社会的公平？ <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 毛泽东是不打高尔夫球，但是他爱游泳，为了他游泳就得封掉一大片海域，还有他在全国有无数的行宫，记得去韶山的时候还看到了为他专门建造的别墅，宽阔，高大，经过特别的设计，就是在炎炎酷暑，不用空调，也很凉快，连洗手间也是特别设计的，哦，还有，还有为他专门设计的防核战的地下宫殿。北戴河别墅区，青岛八大处别墅区在军阀混战时代，在日据时代抑或国民党蒋介石时代平民百姓还可以过过路饱饱眼福，但到了毛泽东时代就成了“非高干不得入内”了。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 当然这不会是老毛自己掏腰包，也不是给某一个国家职位相应的待遇，而是完全为毛个人所造。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 他是不坐飞机，但他坐火车周游全国，会导致所有相关线路的列车停驶，这造成的损失不比为他专门配一架空军一号来的大？ <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 他要与天斗与地斗与人斗，其乐无穷，结果饿死几千万人，难道不是纵个人私欲，置人民于不顾？<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 把中国看成他自己个人的东西难道不是最大的腐败，整个国家就是他的，他只需要保障手中的权力就有一切，他要钱干什么？普天之下莫非皇臣，率土之滨莫非皇土，难怪毛新宇说他爷爷的手是从不碰钱的，嫌钱太脏手。但具有讽刺意味的是，毛打倒了中国大陆所有的资本家，他老人家自己却当起了百万富翁(现在已经是亿万了)来了。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 看看文革期间，他的老婆，女儿，侄子们的显赫位置 20几岁就成高干。那是他们凭自己的能力和资历应得的位置吗？就说那个现在很“低调”的李纳吧，她在文革中也是风云人物。解放军报的负责人，代替中央政治局的中央文革小组办公室负责人是什么级别大家不会没有感觉吧？这不叫以权谋私叫什么？ <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 什么是最大的腐败？不是贪污了多少金钱，打了几场高尔夫球，最大的腐败是权力的腐败，是用人的腐败，是在生活上和权力上的随心所欲。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 那个时代有“良好的社会保障医疗保障、免费的教育、免费的住房？”，说这样话的人不但没有在中国的农村生活过，而且对农民的生活可以说是一无所知。他们说的仅仅是只占中国人口10%多点的红色贵族的生活. <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 先不说占人口80%的农民与这一切社会保障无缘，任何一个在八十年代已经是懂事年龄的人都会知道所谓住房分配里面包含了多少腐败和猫腻，哦，是，这已经不是毛泽东时代了，算是那个时代的残余吧，用这个例子只是想给大家一个感性认识。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 毛泽东时代的教育是免费的么，至少对于农民不是这样，我的舅父和母亲就是因为家里没有钱只能供一个人上学，结果作为家中最小的母亲考上了大学，而聪明一点不亚于母亲的舅父只能中途辍学，就只能一辈子当农民，要是放在现在，识文断字的舅父即使没有完成学业，也会有更多的机会寻找更好工作机会，就象现在他的儿子们一样。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 老是有人说毛泽东为平民某福利，不知道当时的农民到底得到了多少实际的好处，我就不说文革，三年自然灾害那些“非常年代”带来的灾难了。农民几十年被束缚在自己的家乡, 没有自己的土地, 不能按照自己的意愿自由经营, 不能出外工作, 还有国家强行制定的&quot;工农业剪刀差&quot;的剥削。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 毛泽东时代也许是办了一些基础教育，不过，人们似乎忘了，知识学了是要用的，如果大多数的农民，即使聪敏勤奋，却被制度限制在土地上，不能寻找能发挥自己能力的机会，那么那些教育到底给农民带来了多少实际的好处也就只有天知道了。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 统购统销，工农业剪刀差使得所有的农民几乎都成为赤贫，对那个年代的残余还有记忆的人不会不知道当时农民要得到一个城市户口是多么的困难，农村户口，城市户口，集体企业，全民企业，在民众心中是有严格等级区别的，谁说那个时代是公平，公正的？ <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 一个平常的农民子弟要获得一份城镇里面的工作简直比登天还难，要和城市居民通婚，这也是天然的幛碍，我邻居一个同学的哥哥因为看上一个农村的女朋友，和家里闹得不亦乐乎，因为那时候的规定是子女户口随母亲，一个农村的母亲就意味着孩子是农村户口，除了考上大学，几乎没有出头之日。 <br />
<br />
