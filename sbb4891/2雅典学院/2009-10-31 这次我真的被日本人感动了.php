我来讲一件亲身亲历的故事。<br />
那次还是在日本大阪的环球影视公司UNIVERSAL STUDIOS游乐场里。参加的游乐项目是观看立体电影，施瓦辛格主演的《未来战士》，立体效果自不待说，有声光电增强效果，有真人战斗追逐肉搏，有动态模型，有干冰雾气，有洒水吹风，非常热闹。当然，动态的座椅扶手有中英文按钮插孔耳机。很周到。<br />
我们跟在一个欧洲大个子身后进了影剧院。<br />
