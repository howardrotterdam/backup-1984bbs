													<h2>老歌：为什么我们要像狗一样的出国？</h2>
						
						
																											<div id="postmessage_213562" class="t_msgfont">老歌：为什么我们要像狗一样的出国？<br />
　　文章写的有点迟。前几天，一位清华学生发表了他对出国的热切渴望并详列理由，受到追捧回应。俺一度也想写上那么一篇，来谈谈大学生为啥要抢着出国。笔者目前在国内工作，北大读完本科后从业两年。<br />
<br />
<br />
　　中大学生历来是关注焦点，任何新闻，只要和大学生扯上了，都是热门。前段日子，有个记者叫陈杰人，一度成为知名人物，他也没做啥大事，就是披露了武汉女大学生“陪聊”的事情。几乎是同一时期，卫生部副部长宣称中有六百万以上的妓女，关心的人却寥寥无几。<br />
<br />
<br />
　　大学生，在中历来被视为纯洁、真诚的象征。仿佛人一进了大学，就高尚富贵起来，与众不同了。大学生卖肉，大学生行骗，大学生贪污腐败，女大学生傍大款，女大学生卖淫，个个都是众人关注的焦点，主角换个身份大伙就视而不见。<br />
<br />
<br />
　　大学生如今热衷出国，众人皆知。在中，有出国权的人并不多。年轻人里，除高官富翁的子女，只有理工科大学生――往往还是成绩比较优秀的那种，才有出国的机会。那么多高官的子女，就算留在国内，也是要风有风，要雨得雨。可他们依然义无反顾的出国。大学生又如何？每年大学里出国的，都是成绩最优秀的那批，往往争offer争得头破血流。大学生出国可不容易，苦背GRE，花流水般的钱上新东方，多半还得租房子、等offer、过签证，得历经九九八十一难，随时会有被拒的危险。就这么恶劣的竞争环境，这批本可在国内混得不错的人，依然削尖了脑袋出国，而且数量越来越多。<br />
<br />
<br />
　　俺的大学记忆里，有这么件事儿。大三冬天的日子，托福报名。那时候，托福考试可不像现在那么灵活，一到报名日，就是人满为患，赶上一次报名，非得漏夜排队不可。俺和几个哥们拿着小凳子和报纸，在附近一个报名点旁边守夜。从零点，在寒风中一直等到东方泛起鱼肚白，终于等到人家上班了。因为队伍太长，几百个人混乱不堪，专门拨出了警力来维持队伍。警察花了半个小时，把这几百人的队伍整好了。怎么整的呢？用脚。看看谁没站好，就狠狠地用脚踢他的大腿和小腿，直到把他踢到队伍里为止。几百个学生，清华的、北大的、北外的……凡你能想起的最牛气的学校中的自尊心最高，恃才而傲眼高于顶不可一世的最牛的学生，就咬着牙齿，在那里默默忍受几个警察喝斥、脚踹的社主x教育。<br />
<br />
<br />
　　这是为了离开这个国度所付出的代价之一。<br />
<br />
<br />
　　中人对北大学生和清华学生有个最大的误判，他们以为，北大学生和清华学生是不同的。例如北大是理想主义的，清华是实用主义的。北大学生是反抗型的，清华学生是乖乖型的。北大学生是自由化的，清华学生是爱政府的。北大学生是个人主义的，清华学生是国家主义的……其实，在出国问题上，北大和清华学生是完全一致的，不含糊的。唯一的不同是：北大学生一边骂这个社会，一边出国，而清华学生一边赞扬这个社会，一边出国，然后他们之间的绝大多数读Ph.D.，找工作，入美国籍，定居。<br />
<br />
<br />
　　俺在清华也有几个好友，97年，清华有个响亮的口号，叫“为祖国健康工作五十年”，这句话琅琅上口，有气势，清华小伙很爱喊，直到他们出国为止。俺在清华的朋友，在美国建立了庞大的同学会，留在中倒显得孤零零了。<br />
<br />
<br />
　　中知识分子最是忍让。他们秉承了中人吃苦耐劳，小富即安，嫁鸡随鸡，百忍成精的优良传统，院士王选转述领导人的话说：中知识分子价廉物美。两千块钱的工资，就可以随意使唤。中知识分子安于现状，能够忍贫受饥。适应能力比蟑螂更强，在金星上也能生存。近期报导的陆步轩，从一个北大中文系高材生，适应成一个卖肉屠夫，这样的生活现状也没有让他成为土匪或是人肉炸弹。中的知识分子就是这样善于忍受，只要一点点尊重，一点点慰籍，一点点利益，他们就可以在中呆下去。可还是呆不下去。<br />
<br />
<br />
　　中对待知识的态度很奇特。比如说，一个工人，每个小时可以生产出十元的产品。一个受过良好教育的工程师，改良了机器、流程、管理，于是一个工人每个小时可以生产出一百元的产品。那么这多出来的九十元算是谁的功劳呢？西方人对此争论不休，有些人说，工人产出的是十元，工程师的价值当然是九十元；有些人说，工人也提高了效率和劳动强度应该得五十元，工程师五十元比较公平。但中人会说：我们工人的产量增加了，感谢领导们对工人的指导，对工程师的培养与栽培。这九十元是领导的功劳，剩下的十元，请尊敬的工人同志和尊敬的工程师同志平分吧。<br />
<br />
<br />
　　以史为鉴之五十年代：华罗庚――建国来待遇最好的理论数学家。<br />
<br />
<br />
　　华罗庚算是那个时代混得最不错的知识分子之一，他天分极高，不到二十岁就在《科学》杂志上发表论文，后从事数论研究。二十六岁成为剑桥大学访问学者。中日战争爆发后，在中形势最恶劣时回国于西南联合大学任教。中日战争结束后，受聘为美国普林斯顿大学教授。共和国成立后，五零年，放弃国外的优越待遇回国。议定好的年薪是八百斤小米，当然后来没有全给。这位已发表过两百多篇论文和专著的数学家在新中继续从事研究工作。由于华罗庚对政治不感兴趣，所以在纹革中没有受太大冲击。虽然他被拉进了政协，但实际上没有对政治发表过只言片语。<br />
<br />
<br />
　　在1968年，仲共中央组织部部长郭玉峰在h代会上发表了《关于四届全国政协常委会委员政治情况的报告》，在该报告中，他指称74名全国政协常委会委员为叛徒，叛徒嫌疑，特务，特嫌，国特，反革命修正主义分子，里通外国分子等，占159名政协常委会委员的47%。建筑学家梁思成、生物学家童弟周、桥梁专家茅以升相继被打倒。<br />
<br />
<br />
　　但华罗庚却幸免于难。在数学家群体当中，他是最风光的一个，他是中数学界的泰斗，中科院数学科学研究所所长，他很聪明，用一个在理论数学上毫无学术价值的“优选法”，来证明自己“贴近工农”，并在纹革时期赴全国十八个省份讲演做专题报告，而其它的数学家此时大多在牛棚里度过余生。<br />
<br />
<br />
　　这个当时在全国算是最走运的一个数学家华罗庚，生活是怎样的呢？五零年以后，他再也没有能发表出有份量的成果。是条件不够好吗？从纵向比，抗日时期，他在西南联大，物资极度紧张，住在猪圈旁边，他依然可硕果累累。可五零年以后，生活条件好转，可他却出不了成果了。从横向比，被他指责为“贪图享乐不回国”的同龄人，大数学家陈省身，在国外屡屡突破，一举获得数学界最高荣誉之一沃尔夫奖，退休后衣锦还乡到南开大学享福去了。<br />
<br />
<br />
　　华罗庚五零年，毅然放弃优异待遇回国时，发表热情洋溢的爱国宣言：“良园虽好，却非久居之地”，影响了一代海外学人。十年后，他黯然对夫人说：“我想自杀。”消息传出，又影响了一代海外学人。<br />
<br />
<br />
　　他没有精力再搞研究，因为他的同事处心积虑地揭发他，批判他。他二十年的手稿被红卫兵抄家后付之一炬。他放弃了自己喜爱的数论研究，放弃了自己的天赋，去搞应用数学和爱国主义教育。<br />
<br />
<br />
　　统筹法让他摆脱了“脱离群众、脱离工农”的口诛笔伐，使他获得全国巡回演讲的殊荣。可他自己知道这东西的生命力，纹革以后，再也没有人用。<br />
<br />
<br />
　　他在海外的名声为他赢得了待遇，因为他是统战对象，是模范表率，所以要照顾他。可是其它人就不是了。他的儿子，一家四口人，住十四平方米屋子。他最得意的徒弟陈景润，四人住七平方米一间的宿舍。清华大学纹革委员会主任迟群不断关心他的生活，陈景润成名之后，迟群不遗余力地动员他积极展开批判华罗庚的工作。<br />
<br />
<br />
　　华罗庚的优选法在辽宁省做成果展示时，主持辽宁h政军工作的毛远新（毛|侄子）对这位天才数学家高屋建瓴地指出：“优选法的‘最优’是不可能存在的，最优这一提法不科学，不符合R列主x，最多只能称为较优。”于是华罗庚只好带领他的弟子们连夜加班，将展示板里的“最优”统统改成了“较优”。<br />
<br />
<br />
　　这就是在国内待遇最好的数学家的遭遇。<br />
<br />
<br />
　　以史为鉴之七十年代：袁隆平――建国以来贡献最大的农学家。<br />
<br />
<br />
　　八十年代之前的二十年里，中人是在饥饿中度过的。最有名的三年饥荒，按现在俺手头搜集的全国仅17个省的统计资料，加起来就饿死了2100万，预计全国的统计数字应该在2700-3000万之间。不过，俺认为统计数字肯定有问题，算少了。三千万是啥概念？全国一共有七十多万个生产大队，一个生产队大约1000人，正常年份，每个生产队每年死亡10-15人，饥荒三年，每年死亡25-30人，全国就会多死三千万人。可俺那地头，老一辈的记忆里，死得可比这惨多了，所以俺认为三千万这个数字，肯定是少了。饥饿的不仅仅是那三年，整整二十年，俺老家的人就没有吃饱过。<br />
<br />
<br />
　　据老一辈说，真正重新吃饱饭，是在七十年代末，以前的稻子是高高的，风一吹就倒，换了矮水稻以后，粮食真是翻了出来。报纸上曾引述农民的话说：“我们吃饱饭，靠的是两‘平’。小平和袁隆平。”袁隆平的水稻南优2号，比以前的水稻单产增产20%，于1973年研究成功，1976年开始推广。八十年代，国际组织给他的奖项多得像米粒一样。中有九亿农民，他一个人，相当于干了两亿农民的活。有人预估，他的种子共创造效益5600亿美元。假设其中分零头给他，那么他的资产就会大致与世界首富比尔盖茨587亿美元相当。<br />
<br />
<br />
　　那么袁隆平的真实情况是怎么样的呢？截至1998年，袁隆平的月工资是1600元。<br />
<br />
<br />
　　由于他做人老实本分，1953年被分配到偏远落后的湘西雪峰山麓安江农校教书。在那里，才华横溢的袁隆平的职称一直没有提升，工资一直原地踏步，房子依旧窄小阴暗，向上爬的机会被他那些会拍领导马屁的同事抢走了。他唯一的幸运是研究水稻。这是大伙吃饭用的东西，属于纹革中保护品种，他住的又偏远，灾难没降临到他头上。<br />
<br />
<br />
　　纹革中他也被人整过，罪名是毛|制定了农业八字办法：水、肥、土、种、密、保、工、管，他却偏偏认为要加一个“时”字。加上整天摆弄那些别人看不懂的瓶瓶罐罐，于是被打成反革命。<br />
<br />
<br />
　　纹革中，他培养水稻的罐子被红卫兵们砸碎，辛苦培育的品种被他们扔到井里，不得不中断研究三年。遭到批斗和毒打。而如今，他的工作又被新的挺毛派红卫兵们，恬不知耻地称作“毛|时代的伟大成就”，有些干脆说是“毛|领导下的成就”。<br />
<br />
<br />
　　各位出国的老兄，听说过把受害人说成是自己的成就的吗？就像张志新，被辽宁h政班子割了喉管枪决。平反以后，辽宁省官员也声称“张志新同志的伟大精神是辽宁的光荣”。看看美国，政府给企业提供那么好的发展环境，可你有听说过美国把Intel奔腾芯片叫做“克林顿时代的伟大成就之一”的吗？1979年，美国圆环种子公司总经理威尔其惊叹中的水稻成就，向中农业部的官员咨询杂交水稻的发明人是谁，他要签约用高价向发明人申请专利使用权。对此，中种子公司官员义正言辞地回答说，这个发明专利权属中国家拥有。农业部种子公司就是代表国家享有这一权利的唯一代表。要探讨杂交水稻技术转让问题，无须再找“别人 ”。<br />
<br />
<br />
　　1980年，圆环种子公司向中种子公司支付当时可谓是天价的20万美元首期专利转让费，袁隆平一分未得。为配合本次专利转让活动，袁隆平以专家身份出访美国做了四个月的技术指导。回国后，他所得的工资数千美元，被农业部悉数收缴，然后重新发给他每天20元人民币的出国补贴。1981年，国家科委、农委重奖杂交水稻发明人10万元奖金。但单位转手分下来以后，袁隆平仅得5000元。<br />
<br />
<br />
　　2003年，袁隆平在几十年多次创造奇迹以后，正式宣布由于研究经费匮乏，他的研究所的最新成果无法试产，将与美国公司合作。<br />
<br />
<br />
　　这就是国内贡献最大的农学研究员的故事。<br />
<br />
<br />
　　以史为鉴之九十年代：大学生――离上流社会最近的人。<br />
<br />
<br />
　　九十年代，不需要从个体身上截取例子。因为九十年代，俺们已经懂事，这不是历史，是在俺们身边发生的现实。<br />
<br />
<br />
　　中的下等人是谁自然不必多说。要工作，他们到城市会被驱赶和盘剥；要开公司，他们没有启动费用；要从政更是痴心妄想，现在买个官比开个公司难多了。唯一改换身份的出路是上学，如果子女碰巧有天资、能考试，那么就是一个希望。俺就出身这样一个家庭，城市的朋友，都不明白，为啥有些农民，付不起孩子上学的钱，会自杀。上不起学，打工去不就行了吗？事情不是这样，考上大学，不仅仅意味着更好的机会，它意味着跳出了老鼠的儿子要打洞，一代代的农民，一代代的受苦的循环。近几年的教育高收费，将这条路也渐渐堵塞。在俺念的北大计算机系，97级本科有一半农民子弟，而01级本科生，已经基本没有农民成份了。<br />
<br />
<br />
　　但上大学，并不意味着进入中产A或是上流社会，特别是在扔个砖头都可以砸倒几个博士的时代，大学生的价值越来越小。在国内，摆在大学生面前的出路，一条比一条难走。唯一越走越宽的道路就是傍大款，因为有钱人越来越多。傍大款这个词，现在已经不流行了，流行的是做小秘和包二奶，充分体现了中文化博大精神，与时俱进的风格。但这条路毕竟只有少数人可走，绝大多数还得工作，就算读研暂缓几年，工作还是免不了的，总不能读书读到死吧。<br />
<br />
<br />
　　今年回了一趟北京，真是在招聘会上开眼了。俺也算是有一定阅历的家伙了，可从来没见过这么拥挤、这么多大学生红着眼睛左冲右突的招聘会。这几年经济增长得很快。可别的国家经济增长，伴随的是股市行情飚红，就业机会遍地都是，低收入群体得到更好保障。可中的经济增长却是反其道而行之，这钱都到哪去了呢？招聘会结束了，几天以后，消息下来了，本科生三四千，研究生四五千，博士生六七千，像狗一样的找工作虽然和像狗一样的出国有所相似，可一个卖得贱，一个则卖得贵些。现在有些人觉得中的经济环境很好啊，他们的理由是：经济环境不好，外资怎么刷刷地就进来了呢？这还用废话吗？像垃圾袋一样便宜的大学生劳动力，没有法律保障的工作时间，法官不是腐败的就是向着资本家的，还不让工人自己组织工会。这个大中，不摆明了是外国资本家天堂中的天堂么？可俺们，迈向上流社会的大学生们，环顾四方的时候，又发现自己是在哪里呢？以上是俺要说的话，但愿对已经出国和想出国和不想出国的大学生们有用。<br />
<br />
<br />
　　作者:老歌</div>

							
							
							
							
