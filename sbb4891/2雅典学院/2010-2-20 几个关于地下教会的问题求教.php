																											<div id="postmessage_486740" class="t_msgfont">楼主所谓的“地下教会”可能指的既包括了家庭教会也包括了地下教会。在基督新教一般称家庭教会而非地下教会。地下教会一般用来指称直接服从梵蒂冈教廷领导而非天主教爱国会领导的天主教教会。<br />
<br />
1. 规模：我对天主教地下教会的情况不很了解，但根据维基提供的数据，人数约1000万。具体请参阅<a href="http://zh.wikipedia.org/wiki/%E5%9C%B0%E4%B8%8B%E6%95%99%E6%9C%83" target="_blank">http://zh.wikipedia.org/wiki/%E5%9C%B0%E4%B8%8B%E6%95%99%E6%9C%83</a><br />
对于基督教家庭教会的教众数量，暂时无法统计，但私以为数量是相当之庞大，而且增长速度是非常之快的。根据2008年The Economist print edition中Son of Heaven一篇所提及，根据“China Aid Association (CAA), a Texas-based lobby group, the director of the government body which supervises all religions in China said privately that the figure was indeed as much as 130m in early 2008. ”私以为1亿三千万这个数字还是比较靠谱。<br />
<br />
2. 我觉得这样的数字是很正常的，并且认为基督徒是未来改变中国社会的一股不可忽视的重要力量。现在中国基督教徒“三多”（农村人口多，老年人多，文化程度较低者多；也有称妇女多，老年人多，文化程度较低者多）的现象仍然非常严重，如果你去当地的官方教会，尤其可以发现这种情况。但近几年，城市，尤其是大中城市的家庭教会发展速度非常迅猛，而且有非常多的知识分子和城市中产者接受基督信仰，而且还有数量更为可观的“匿名基督徒”。<br />
<br />
<br />
3.先扯扯题外话，“反政府”这个词私感觉真有中国特色，在中国语境之下，请楼主区分“反政府”，“反革命”还有“颠覆国家政权”的区别，以便回答这个问题。<br />
对于天主教地下教会是否具有“反政府”色彩，我确实不知道，但私以为怎么样也不能以“反政府”这样的标签冠之。但对于基督教家庭教会，我了解到的一些绝不会“反政府”，但如果当政府强奸你的时候你不反抗，政府做邪恶的行为之时你不发声，政府不履行自己承诺时你熟视无睹，还为政府辩护……私以为这样根本不是《圣经》所谓“顺服”。反之，如果你要说当政府强奸你的时候你反抗，政府做邪恶之事的时候你发声，政府不履行承诺之时你要求它履行，若这样就是“反政府”，那么我想不少家庭教会确实“反政府”。刘晓波，谭作人，胡佳等人都“反政府”，因为他们都“颠覆国家政权”。<br />
<br />
4. 基督宗教的宗派实在太多，主要有罗马公教（天主教），东正教和基督新教，而在三大教派内又有不同的宗派，数量相当庞杂。按照信徒的神学立场分类又有基要派，福音派和自由派三大派。我想判断是否异端的根据必然是《圣经》，但不同人对于《圣经》里面的话语都可以有不同的解读，所以也难免产生互相论断的情况，就比如某些新教宗派指责天主教是异端。私以为这种互相指责来确定自身正宗的行为实在是无聊至极。但有些确实是异端，比如东方闪电，这是被基督教各教会公认的异端，它的教义明显违背《圣经》的教导。所以判断异端我觉得还是以《圣经》为根据，当然判断之前你也要有一定的神学素养才行。<br />
<br />
欢迎批评、指正。</div>

							
							
							
							
