																											<div id="postmessage_312161" class="t_msgfont">再谈董乐山不原谅董鼎山<br />
<br />
老曹<br />
<br />
在我自己的思想成长过程中，受到的来自中国作家的影响很有限；在这有限的几个人中，翻译家董乐山先生是一个。不是被他本人的作品，而是被他的翻译作品。几本有限的关於极权社会的英文著作，如夏伊勒的《第三帝国的兴亡》、库斯勒的《中午的黑暗》、奥威尔的《一九八四》、《动物农场》等，竟然都是董乐山先生翻译的。我在八十年代中期读《一九八四》时所受到的震撼至今难忘，而由那本书所产生的对极权社会的理解和痛恨，远超过我本人由於在GC社会的生活体验而带来的对毒裁砖制的认识。所以对翻译介绍这些作品的人，一直心存感激，更有一份和译者的无言的共鸣。<br />
<br />
今年1月16日是董乐山先生去世十周年。他的哥哥董鼎山先生在上期《开放》撰文，再次提到他和弟弟因思想差异而产生的争执和隔阂，而且董乐山到死都没有原谅董鼎山的「为TG张目」（董鼎山引董乐山语） 。董鼎山为弟弟没有原谅他而伤心，同时耿耿於怀地再次指责董乐山是「在极权社会中成长的知识人士，往往养成一种看事物很极端的态度」，他认为这种态度是「非黑即白」，然後给扣「瘟革陋习」的帽子。<br />
<br />
董鼎山自己「帮TG讲话」（董乐山语），除了董乐山会把他「厉声大骂一顿」之外，别人也并没和他过不去（那种左倾、反美的东西，当今亲G海龟写得比董鼎山更「有质量」）；可是他自己却一再撰文批评别人反G、亲美的观点是「非黑即白」，并给扣瘟革帽子。就此，我曾在零三年写了题为「在大是大非面前没有灰色地带」的文章，指出「董鼎山所做的，是用汗水的功夫，抵消著董乐山用血和泪所做的努力。怎麽应该是董乐山给董鼎山道歉？完全应该是董鼎山给董乐山道歉才对！」<br />
<br />
●《阿特拉斯耸耸肩》影响力仅次於《圣经》<br />
<br />
五年过去，董鼎山不仅依旧相当左倾，仍对董乐山对他的愤怒不能理解，更对我的批评无法释怀。不过他在上期《开放》文中贬义地指我「对TG嫉恶如仇，犹如乐山」倒让我感觉很褒奖。本无意再撰文，但由於董鼎山再次提到董乐山的不原谅他，使我联想到美国著名哲学家、小说家安．兰德（Ayn Rand）对她妹妹的绝不原谅。因尚未看到中文世界有人提及这一段，所以觉得值得一叙。<br />
<br />
安．兰德在21岁（1926年）时离开苏联来到美国，从此用英文写作，成为二十世纪最影响美国人思维的、最受大众欢迎的小说家（不是「之一」）。她的《阿特拉斯耸耸肩》（Atlas Shrugged）在美国国会图书馆和全美最大读书俱乐部1991年联合举办的「最影响你人生的一本书」的问卷中，排名仅次於《圣经》。她对极权世界对人类的践踏，痛恨到没有一毫米可原谅的馀地。由於她对西方左派的毫不妥协的、黑白分明的痛斥（称西方左派是为虎作伥的爪牙），所以从来都被以亲G、亲左、反美为压倒多数的西方知识份子们严重排斥。这是中国人直到近年才听说这个名字的原因。<br />
<br />
安．兰德离开苏联後，前十多年还和家人保持通信联系，二战後则音信断绝；後来听说家人全部在战争中被炸死。事实上她最疼爱的、和她最趣味相投的小妹妹诺拉还活著。七十年代中期，诺拉在一本美国杂 关於当代名人的介绍中发现了姐姐安。她马上给姐姐写了一封信，寄到美国的杂 社，请他们帮助转寄。安在接到编辑部打来的电话、说她的小妹妹还活著并寄了信来的时候，几度喜极而泣。她随後设法把47年没见面的妹妹（和妹夫）接到了纽约，在自己住的楼里给他们租了带高级家俱的公寓，并准备在新泽西俄国人社区给他们买房子，让他们永远留在美国。<br />
<br />
●兰德为何跟重聚的妹妹反目<br />
<br />
但姐妹相见後立刻发生了冲突。倍受GC洗脑的诺拉认为所有外人，包括安请的司机、佣人、朋友等，都是监视她们的间谍。她虽然也觉得苏联缺少自由，但同时又认为，「自由对我又有什麽好处呢？我也不是政治活动家。」而且诺拉迅速就开始抱怨美国，认为纽约脏乱差，还不断说列宁格勒有多好。安一开始还努力试图给她解释，但很快了解了诺拉的不可救药。尤其是会英文的诺拉竟然对姐姐那些对无数美国人起到了巨大影响的书毫无兴趣去读，这也让安很伤心失望。姐妹俩关系迅速恶化到几乎无法对话。结果六个星期後，诺拉夫妇就决定回到苏联。从此姐妹俩再没联系。而且後来一提起诺拉，安就十分愤怒。<br />
<br />
在这个故事里，最具讽刺意义的是，诺拉跟谁抱怨美国，也不应该跟那个比全世界任何人都更热烈推崇美国价值的安.兰德抱怨；跟谁赞美苏联，也不应该跟那个和极权世界不共戴天，不仅对苏联，对整个俄国都绝不说一句好话的安．兰德去赞美。董鼎山、乐山的故事，也具类似讽刺∶海外文人尽可撰文亲G左倾，却最不应该是那个对砖制厌恶至极、中译了最多反极权著作的董鼎山的弟弟。<br />
<br />
安和诺拉姐妹俩的反目，虽然和董鼎山、乐山的反目相反，是住在自由世界的姐姐不原谅住在极权世界却为苏联辩护而不珍惜美国的妹妹，但从根本上来讲是同样的，那就是∶痛恨极权社会的一方，不能原谅对毒裁社会的任何辩护；对砖制政党，更没有馀地。原则高於血缘和亲情。没有对极权社会的深恶痛绝，就不会有对自由像呵护生命一般的珍惜和不顾一切的追求。<br />
<br />
●夏志清才是真正的了不起<br />
<br />
相当一批享受自由世界的华人，都拐弯抹角地为砖制的存在辩护。在董鼎山那一辈人里，亲左、亲G是普遍，不是异数。但异数绝对有。我零三年那篇批评董鼎山的文章发表後不久，在一个会议上见到夏志清先生，他马上对我说，「你骂董鼎山的文章写的好，写的好；了不起，了不起。」（「了不起」只是夏志清的口头禅而已）。事实上，像夏志清这种根本没受过GC蹂躏的人，能认清砖制本性，坚定反G，才是真正的了不起。 <br />
<br />
和董鼎山同样是在年轻的时候就来到美国，同样是没有亲身体验过GC专制的暴虐，但夏志清的始终如一的坚定、坚决、义无反顾的反G，一直令我十二分敬佩。夏志清和董鼎山都住在纽约，年龄也相仿（夏志清1921年出生，董鼎山1922年），两位的同样高藓退嘉活跃，倒是同等令人佩服。但根本不同的是，凡是和反G有关的会议，夏志清是每请必到，而董鼎山则从不见踪影。当然，或许大家都知道他的立场，也不请他。<br />
<br />
