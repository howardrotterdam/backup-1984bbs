																											<div id="postmessage_312158" class="t_msgfont">在大是大非面前没有灰色地带――董乐山为何不原谅董鼎山<br />
作者：曹长青<br />
<br />
按照西方新闻界的一个不成文的默契，为同一家报刊撰稿的专栏作者，一般不会公开撰文相互批评，尤其是在他们同是专栏作者的报刊上。虽然中文作者经常有不在乎任何规矩的做法，但我自己觉得有道理的，总是试图按西方规矩去做。但在西方生活了半个多世纪、对西方媒体似并不太陌生的董鼎山先生，在《开放》9月号上，以读者来信的方式，指责我在“右派来自火星，左派来自水星”（《开放》8月号）一文中指出“西方左派反美、反资本主义”是中国文革式的做法。他认为我的观点“非黑即白”是“文革陋习”。 <br />
&nbsp; &nbsp; 什么叫文革做法？首先，它最关键之处在于，只允许一种观点存在，而用行政手段剥夺别人响应、挑战的权利。今天董鼎山可以在任何报刊上发表挑战、抨击我的观点的文章，我没有任何行政权力，怎么施行文革做法？<br />
　　其次，关于语言。文革语言的明显之处在于没有事实根据的大话、空话，侮辱、谩骂，和乱扣抽象帽子。但是，无论多么恶劣的语言都并不可怕，只要允许被挑战的话。这就是为什么在西方文坛照样有相互非常刻薄的批评、指责，甚至谩骂，但没有人会以为然，因为谁都无法阻止别人的挑战。那些不以事实说话，而只有空话和谩骂的，只能自取其辱。<br />
　　●为什么左派恨美国<br />
　　我来美国这十多年来，一直很注重自己语言的使用，尤其是在批评文章中，力求准确辛辣，但绝不用空话和谩骂。这主要还不是担心“文革语言”，而是因为空话和谩骂没有力量。我已经不止一次见过董鼎山这种讲理讲不过别人，就给扣“文革”帽子的做法，但我不会反过来指责那些乱扣帽子的人才更“文革”，因为我完全可以响应，让读者判断谁更有道理。<br />
　　现在回到我的文章“右派来自火星，左派来自水星”。首先，西方左派（尤其是极左派）反美、反资本主义，这种结论根本不是我的发明，这在西方是常见的现象，常识般地为人所知。关于这方面的讨论有太多的文章了，随便去Google就可以查到一大堆。近年更有畅销专着《为什么左派恨美国》（《Why the Left Hates America: Exposing the Lies That Have Obscured Our Nation\'\'s Greatness》by Dan Flynn），详细地介绍了这些“常识”。中东问题专家，《纽约时报》畅销榜作家派普斯（Daniel Pipes）曾在去年11月12日的《纽约邮报》发表题为“仇恨美国的教授们”（Profs Who Hate America）的文章，点了以乔姆斯基（Noam Chomsky）为首的几个左派大学教授的名字，指出他们是仇恨美国。<br />
　　美国最坚定反对共产主义、最热烈推崇资本主义的著名哲学家、小说家安．蓝德（Ayn Rand）曾说，左派知识分子是为虎作伥的“爪牙”。美国另一位著名哲学家胡克（Sidney Hook）说得更彻底：西方左派是人类自由的掘墓人！难道他们这种“非黑即白”的、对西方左派的痛斥都是中国的“文革做法”吗？<br />
　　●做思想坦诚的知识人<br />
　　自三、四十年代至整个冷战期间，西方左派同情、向往共产主义，反对以美国为代表的资本主义，不仅不是奇事，而是知识界的普遍现象。在美国生活了半个多世纪的董鼎山先生真的不知道这个事实吗？还是由于他本人多年来一直持左倾观点，所以对我强烈抨击西方左派的文章，无法不敏感？<br />
　　董鼎山先生指责说“非黑即白的态度要不得”。但是，在大是大非的原则问题上从来就没有灰色地带！在恐怖主义和反恐战之间“非黑即白”；在极权专制和民主体制之间“非黑即白”。作为一个思想上坦诚（intellectually honest）的知识人，我从来不回避自己属于右翼，因为我从来不想讨好所有人群。<br />
　　而董鼎山先生有勇气承认他到底是左派还是右派吗？说自己不左、不右、不黑、不白的人，不是头脑不清楚，就是功利城府太深。为《开放》撰稿的作者，大都在国内无法发出文章，而董鼎山先生则是极少数的可以在国内仍有发表文章特权的作者之一，还有能上《人民日报》（海外版）的“殊荣”。能在专制和民主之间找到平衡点的人，自然不可能是黑白分明的人。<br />
　　尽管多年来我对董鼎山在中国大陆和海外发表的许多明显左倾（甚至亲共）观点，都持反对态度；尤其是由于他是我非常尊敬的中国著名翻译家董乐山先生的哥哥，而他们兄弟之间的对共产社会认识的巨大差异，曾多次令我欲撰文评论。但碍于同是《开放》专栏作者的原因，一直未提笔。现在董鼎山先生率先打破规矩，那我就不会客气。<br />
　　●骨灰不留在专制的土地上<br />
　　在西方华裔中，类似董鼎山这种多年左倾、却从不反省的文人大有人在，但我认为这里面董鼎山是最不可原谅的一个，就因为他是董乐山的哥哥。在中国所有的翻译家中，董乐山是最令我敬佩和感动的，因为他翻译了威廉．夏伊勒的《第三帝国的兴亡》、库斯勒的《中午的黑暗》、奥威尔的《一九八四》、《动物农场》和《奥威尔散文集》等这些深刻揭示极权社会黑暗的作品。只有对极权社会彻骨痛恨的人，才有可能用心血翻译这些作品。在他那一代中国知识人中，有翻译能力的大有人在，但明显地是，能清楚地意识到这些作品价值的人屈指可数，否则怎么这仅有的几本关于极权专制的书，竟然都是董乐山先生翻译的！<br />
　　董乐山先生能选择翻译这些作品，绝不是偶然的，完全是出于他对极权社会的清晰的认识。生活在专制下的董乐山先生，在专制和自由的选择之间是非常“黑白分明”的。据国内作者的回忆文章，由于他的某篇文章无法通过审查，他曾宁可撤稿，也不按照共产党的意识形态改变观点，他尊严地说“我不会写遵命文学！”一个多么令人肃然起敬的宣言！在那个制度下！董乐山先生去世后，连骨灰都不留在专制独裁的土地上，让亲人带到美国；其对专制和自由的黑白真是太分明了！<br />
　　●自由人不能为监狱辩护<br />
　　相比之下，生活在自由世界的董鼎山先生，却曾用许多文字在中共的报刊上对西方资本主义社会热嘲冷讽。在七、八十年代，董鼎山是少有的几个能在中共报刊上发表文章的海外华人作者，其观点多附合中共意识形态（当然，否则怎么发得出来！）西方当然有许多可抨击之处，但在共产党的报刊上发表，其迎合中共宣传的意义一目了然。鼎山、乐山两兄弟不和，是董鼎山本人也承认的事实，而这种不和主要是在对中共的观点上。直到1997年，董乐山还指责董鼎山“为中共讲话”。据2001年《开放》杂志对董鼎山的采访，他表示由于董乐山至死都没有向哥哥说句道歉，所以他很伤心。当时读到这段文字，我很吃惊，怎么应该是董乐山给董鼎山道歉？完全应该是董鼎山给董乐山道歉才对！<br />
　　在我上大学的年代，通向海外的窗口是极为有限的，在那少得可怜的信息中，董鼎山那些对西方社会的偏见文字就占了相当的份量，所以很扎眼。他的那些关于美国的介绍，基本没有超出中共意识形态的轨道，那是一种潜移默化的附合共产主义思维的麻醉剂。而董乐山的作品给人的是，启迪灵魂、认清共产主义邪恶的清醒剂。1986年我在深圳第一次读到董乐山翻译的《一九八四》时，那种巨大的震憾至今难忘。当还没有完全理解和消化之际，就迫不及待地在《深圳青年报》上发表了一篇题为“官僚化、极权化使世界变得黑暗”的书评，急切地希望更多的读者去看这部著作，该文最后一句话是：“请君一读，定会感慨万千！”<br />
　　近年来，董鼎山的文字有了一定的转变，对中共有了几分含蓄的批评，但他不仅从来没有反省过当年那些附合中共意识形态的文字，也没有改变其左倾观点。当然，那些观点从根本上是来自一个源头的。<br />
　　其实，简单清楚地归纳一句话：董鼎山所做的，是用汗水的功夫，抵消着董乐山用血和泪所做的努力</div>

							
							
							
							
