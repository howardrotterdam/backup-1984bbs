													<h2>南风窗：董乐山在1984</h2>
						
						
																											<div id="postmessage_311482" class="t_msgfont"><img src="http://i37.tinypic.com/2cexc6.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
今年是《1984》正式出版60周年，也是它的中文翻译者董乐山去世10周年。<br />
从年轻时投身地下党运动，到晚年笔耕不辍翻译西方人文主义著作，这位耿直、孤傲的老人一生都在坚持理想。不同的年代有不同的方式。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;特约记者 冯翔 北京报道<br />
<br />
“四月间，天气寒冷晴朗，钟敲了十三下。”<br />
这是《1984》的第一句话。<br />
写下这句话时，英国人乔治.奥威尔一定不会想到，这部小说将被翻译成60多种文字出版。<br />
将这句话翻译成汉语时，中国人董乐山却很清楚地知道：这部小说将在世界上人口最多的国家掀起巨大的反响。<br />
作为世界上最著名的反乌托邦、反极权的政治隐喻小说，《1984》于1949年在伦敦出版，1979年在中国以内部资料形式首次刊印。它的翻译者董乐山认为：翻译这本书是他作为社会主义者，对信念的一次坚持。正如他年轻时参加中共地下党运动一般。<br />
<br />
理想引导全家<br />
<br />
单纯从家世来看，董乐山无疑具备了做一个无产阶级革命者的充分条件。<br />
1924年出生在一个逐渐没落的宁波中产商人家庭，从小接受良好教育；排行老三，自比《家》中叛逆的觉慧；才华横溢，具有强烈的爱国热情……<br />
如果有人硬要从履历寻找什么先兆的话，那就只有他的学历了：上海圣约翰大学，来自美国；他死后漂洋过海安息在那个国家。英国文学系，对多年后他翻译来自英国的《1984》而言，也许只是个神秘的巧合。<br />
1940年，在上海读中学的董乐山参加了中共地下党组织，对沦陷区学校去汪伪政权登记注册并悬挂汪伪国旗发起激烈抗议，因此被开除。他并未气馁，更把弟弟发展为交通员，到民间传播抗日思想。<br />
要是不发生剧烈的朝代更替，董乐山的一生很可能会平静地度过。他还不到二十岁，便以“麦耶”为笔名，蜚声上海戏剧评论界。得了稿费，他交给五妹董木兰一张支票，让她买条裙子。<br />
直到现在，董木兰还为这个额头宽宽戴副眼镜、一身书生气的三阿哥对她的关爱，而感觉自豪和温暖。然而她承认，自己并不了解他的内心世界。“他一辈子都把我当小妹妹，到死都是这样。他这人，一旦认定什么事就很难改变。”<br />
抗战结束后，在圣约翰大学英国文学系就读的董乐山等人又不断去剧院散发左翼传单，发动工人“反内战，反饥饿，反迫害”。直到他单线联系的直接领导被捕，与党组织失掉联系为止。<br />
1949来了。许多朋友劝董乐山去香港，他拒绝了，留在了大陆。他并非职业革命者，但从当时他手不释卷的两本书《新民主主义论》《论联合政府》来看，至少心向社会主义，拥护共产党追求民主、自由，是毫无疑问的。<br />
这一年的6月8日，《1984》在伦敦出版，大获成功。但董乐山并不知道它，就算知道了也不会有心思翻译。他正忙着引导全家，投入对这个新政权的服务中。1950年，他考上新华社外文部，马上一封信把董木兰从上海叫去：“只有参加革命才是出路”。<br />
过了两年，四弟董名山从解放前三哥推荐他报考的“共产党活动中心”――浙江大学毕业，也分到北京。80岁的时候他还对哥哥当年的话刻骨铭心：“一旦你入了党，就要一辈子为共产主义理想服务。”<br />
几年后，董木兰以“革命干部”身份，给在美国的二哥董鼎山写了一封信：“你现在还在我们伟大祖国的头号敌人美国帝国主义那儿工作，不回来参加祖国的建设，我要坚决和你划清界线，从今以后我和你断绝关系，你再也不是我的亲阿哥了。”<br />
从这封信后，兄妹二人一直未通信。直到60年代节粮度荒岁月时，二哥托人从香港给董木兰一家几次寄来奶粉、猪油、白糖等食品，保证了她一家人的生存。<br />
<br />
苦中作乐<br />
<br />
1957年，刚在“八大”上与钱钟书等人一起担任过英文翻译的董乐山被划为右派。直接原因是：他主张职业自由，开放职业市场，并认为苏联的产品质量不好。<br />
敢对“老大哥”提意见！这成为他“向党进攻”的罪证。如果他有先见之明，就该等到几年后中苏两国交恶时再提。那时不但没事，还能证明他的高瞻远瞩。当然这只是设想。“董乐山那人性格特别耿直，孤傲。这样的人不当右派，谁当？”他的妹夫，同时被划为右派的伍文煦说。<br />
被剥夺翻译权利的董乐山当了一名基础英文教员，后又在新华社打下手管理资料。在地下仓库里，他发现了一本厚厚的英文书：美国记者威廉.夏伊勒的《第三帝国的兴亡》，当做垫脚的砖头泡在水里。两个星期内，他读完了这本一百三十万字的著作，马上通过朋友向当时的世界知识出版社推荐。<br />
由他领衔翻译的这套书在1963年出版。由于书中对斯大林颇多不敬之语，暂时内部发行。但仍产生极大轰动，一时在“内部”奇货可居。连董乐山自己家里留的一套都被借丢了下册。<br />
至今，著名作家胡发云仍用“石破天惊”形容当年阅读该书的震撼，他称：“这本书在意识形态上撕开了一个大口子”，因为“纳粹的那一套做法，和中国的‘文革’太相像了。”<br />
此乃后话。翻译《第三帝国的兴亡》，只是董乐山本人不甘荒废，在政治风暴中苦中作乐而已。据他的儿子董亦波讲，“文革”期间，董乐山有一友人因受不了逼供，胡编乱造，编出一个子乌虚有的特务组织，董乐山的名字也列在其中。他因此被关入牛棚，受到长期关押、审讯和逼供。因为不肯诬告他人，董乐山一直被关到只剩自己一个人。<br />
“有一次，我和我母亲去看他，关他的‘造反派’告诉我们，他还有‘罪行’没有交代，叫我们要他赶紧交代。他见到我们时，当着监视我们的人说：他实在没有什么可交代的了。”董亦波在电子邮件中对《中国周刊》记者回忆。<br />
直到下放河南的“五七干校”劳动时，董乐山因疲累过度，从拖拉机上摔下来断了一只胳膊，这才被允许回到北京。受他影响来到北京工作的四弟五妹，都没有躲过一轮轮的政治运动。董名山质疑苏联出兵匈牙利，董木兰对当时的支部书记提意见，后果可想而知。<br />
后来，有一次相见，董乐山抱歉地对妹妹说：“我对不起你，把你叫到北京来……结果你们都成右派了。”<br />
今天的董木兰说，她并不责怪哥哥。因为在那个年代，“参加革命是非常光荣的一件事。”<br />
<br />
千字4元<br />
<br />
70年代后期，董乐山才恢复工作的权利。这时，他注意到了《1984》。<br />
在新华社工作时，董乐山曾经看到过奥威尔的名字，当时仅知道他是“反极权主义的”。对“老大哥在看着你”“战争就是和平，自由就是奴役，无知就是力量”等句子，并未用心去揣摩。<br />
这一次不同。<br />
“我这一生读到的书可谓不少了，但是感到极度的震撼的，这是唯一的一部。因此立志要把它译出来，供国人共赏。”十几年后他在一篇文章中说。“《1984》在今天仍有价值。……只有彻底否定了……极权主义，才能给我们这些多年为社会主义奋斗的人，带来真正值得向往的社会主义。”<br />
机会很快就来了。1978年，在“延安整风”时曾被两千人大会批斗、后任新华社副社长的陈适五在外文出版局主持一本《国外作品选译》，专门刊登“某些有参考价值而篇幅过长或性质不合的材料，供领导及其他同志参考”，向董乐山约稿，他选择了《1984》。<br />
1979年4-7月，《1984》在《国外作品选译》分三期刊登，这是《1984》同中国读者的第一次见面。五千份的印数，又是内部发行。董乐山得到的稿费是千字4元。<br />
《1984》正式以书籍形式出版，是在1985年。著名学者林贤治――当时花城出版社的编辑――向《中国周刊》记者回忆：那一年，花城出版社的译文编辑室主任蔡女良决定，以内部形式出版一套《反乌托邦三部曲》。这三部曲便是英国作家赫胥黎的《美丽新世界》、前苏联作家扎米亚京的《我们》，奥威尔的《1984》。<br />
除了定价1.75元的《1984》获得了广东地区优秀翻译奖，董乐山本人获得了一座缪斯女神像外，《反乌托邦三部曲》的出版并未引起什么轰动效应，也没有人因此被奖励或惩罚。这一年，全世界有更重要的事要关心：中国裁减百万军队，苏共末代总书记戈尔巴乔夫上台，以及由此带来的一系列“铁幕”的松动。没有工夫注意这样一本老书在中国静悄悄的出版。<br />
同当年引导他参加地下党、为新中国政权服务一样，董乐山这次又送了一本自己翻译的《1984》给刚平反没几年的弟弟董名山。原话是：“你看看吧，这书……挺好。”<br />
<br />
至死不泯的信念<br />
<br />
《1984》给董乐山带来了巨大声誉。由于他，中国读者得以在对这本政治隐喻小说的全球性阅读、理解中，不至于落得太远。<br />
一部分德国人认为：《1984》是对纳粹德国的文学再现；东欧人则认定《1984》是反映的苏联三十年代大清洗；连尼日利亚人都强调，尼日利亚军政府的种种恶行与《1984》暗暗相合。<br />
在中国，一个就读于中国人民大学商品学专业的学生拿到了几本《国外作品选译》。他在回忆中写道：<br />
“1980年，我在大学里读到了乔治&#8226;奥威尔的《1984》，这是一个终身难忘的经历。……但是对我来说，它已经不是乌托邦，而是历史了。不管怎么说，乌托邦和历史还有一点区别。前者未曾发生，后者我们已经身历。前者和实际相比只是形似，后者则不断重演，万变不离其宗。”这个学生的名字叫王小波。<br />
借助王小波的名气，《1984》、乔治.奥威尔迅速成为“中国知识分子的关键词”。至今，《1984》在广州、上海、辽宁等地出了近10个中文版本。跟董乐山所预料的一样，掀起了巨大的反响。即使是“内部发行”的第一版，也同当初第一次刊登它的《国外作品选译》一样，在社会上不停地传借、转手，奇货可居。 <br />
“唐老鸭”唐师曾回忆，1994年初，他借得了一本花城出版社于1988年首次在国内公开发行的《1984》，没到手一周就被他“显摆”，借丢了。如骨鲠喉的他直到6年后，才从一个书商朋友手中弄到两本新的，还给当初借他书的朋友。<br />
事后，唐师曾得知：这次的《1984》是辽宁出的一套丛书中的一册，全套共一百多册。那位书商是拆散了总价两千多元的两套书，把书给他的。<br />
在2008年末的中国，一家南方报纸办的“改革开放30年，中国最具影响力的30本书”评选中，《1984》榜上有名。这一书榜上，还有另外两本董乐山领衔或参与翻译的书：《第三帝国的兴亡》和《光荣与梦想――1932-1972年美国社会实录》。<br />
“3本――30本，十分之一都跟董乐山有关。这是非常了不起的。”多年研究文化名人的《人民日报》著名记者李辉评价：晚年的二十年，是董乐山一生真正的工作高峰期。而《1984》，不过是董乐山这段翻译工作的开端之作。<br />
这段时间的董乐山在中国社会科学院美国研究所工作，基本上以每年一本的速度进行国外名著的翻译或推介工作。《西方人文主义传统》、《苏格拉底的审判》、《古典学》《第三次浪潮》以及反极权主义小说《正午的黑暗》……他还重新翻译了埃德加.斯诺的《红星照耀中国》。四十年代，无数热血爱国青年曾怀揣此书投奔延安解放区。<br />
董乐山的侄子董森林回忆，他去探望晚年的三叔，发现董乐山与妻子各居一室，埋头写作。“桌上堆满书稿，满屋都是（单词）卡片”。这种情形一直持续到他去世。<br />
过度劳累的工作夺取了董乐山的健康。“一位瘦弱的长者，屋子里是一股煮中药的味道”，令当时的来访者留下深刻印象。其间，在美国的二哥邀他出国定居，以休养在政治风暴中备受摧残的身心，他拒绝了。<br />
何以这么累？社科院美国研究所美国文艺研究室副主任潘晓松透露说，当年的董乐山并没有翻译书的工作任务，译不译、译什么、译多少，都是他自己说了算。翻译的方向和效率，都是董乐山自己选择的。<br />
“这些译作中间埋藏着一条思想线索”。林贤治指出：董乐山晚年的译作，全都带有鲜明的指向性，充满人文启蒙的社会责任。<br />
李辉认为，董乐山晚年如此发愤工作，既是因为年轻时的理想没能实现，更是几十年失去工作权利，怀才不遇而积累的情绪。“这种痛苦远比肉体上的痛苦更深刻。”<br />
需要强调的是，董乐山和奥威尔的晚年一样，都以一个忠诚的社会主义者自居，并以此作为工作的思想基础。<br />
林贤治引了两段话，一段是董乐山翻译的奥威尔文章《我为什么要写作》：“我在1936年以后写的每一篇严肃的作品都是直接或间接地反对极权主义和拥护民主社会主义的。”<br />
另一段是董乐山在《正午的黑暗》中所写的译后记：“……光明正大地、毫无隐晦地正视这段历史，让人民和历史作出应有的判断，是任何一个真正的革命者的不可推卸的义务。”<br />
“如果不翻译这么多书，而是好好休息，他不会死那么早。”潘晓松说。“董乐山绝对是积劳成疾。”<br />
<br />
死后超脱<br />
<br />
一生历经坎坷的董乐山，至死也没有改掉耿直和孤傲。晚年还变得更加敏感。<br />
1997年，二哥董鼎山从美国回来。兄弟二人见面十分钟后，董乐山就严词斥责哥哥“涂脂抹粉”“粉饰太平”，二人不欢而散。实际上，董鼎山只是发表了一篇文章，认为中美之间不可能爆发核大战而已。<br />
此时，董乐山曾对董森林说：二十世纪是黑暗、恐怖、杀人和专制的世纪，“我一生的理想已付之东流”。个中缘由，董森林认为是一战、二战、韩战、越战和“文革”等。<br />
“我想他的心灵势必受到了创伤，心情悒郁使他后来患上了不治的癌症。”与董乐山同为美国研究所“三驾马车”之一的翻译家，梅兰芳的儿子梅绍武这样评价董乐山。<br />
1999年1月16日，病入膏肓的董乐山拒绝了最后抢救，静静地在北京一家医院去世。现在，他长眠在美国加利福尼亚州临近太平洋的一个山岗上的陵园里。骨灰安放处座东朝西，面对太平洋遥望中国。“他在这块土地上的使命已经结束。”定居加州硅谷的董亦波说，即使父亲没有“骨灰不留在中国”的遗嘱，他也会这样做的。<br />
那个年代的知识分子普遍命运多舛。包括梅绍武本人在内，“三驾马车”如今已全部去世，病因均为癌症。<br />
今天，翻译界的一种普遍共识是：像“三驾马车”那样的翻译家，那样的翻译行为，再也不会有了。<br />
关键的变化在于社会的价值取向。二三十年前，翻译家想翻译什么，就能出什么。只要出版社的编辑觉得这本书对社会有价值。冷僻，卖不出去？不要紧，出版社有国家补贴，“赔这笔钱，就是为了做社会文化事业的”。<br />
在那个“国家补贴赔钱”的时代，翻译家可以坚持一份自己的取向和尊严。梅绍武是最早翻译俄裔作家纳博科夫作品的中国人，却拒绝翻译纳氏最著名的小说《洛丽塔》，理由是“它讲的是一个老头和一个未成年的小姑娘谈恋爱，我觉得它和我们中国的道德观念不太相符，我不太喜欢它。”<br />
如今，全球化时代，出版社首先考虑的现实问题是――钱。国家拨款已成历史往事，他们必须考虑这本书是否能卖出去，至少印五千册才能保本。光叫好不叫座的书，是绝对不能接受的。“在今天，想说服出版社出版一本很冷僻的西方人文著作，已经是一种奢侈。基本不可能。”一位曾和董乐山合作过的翻译家说。<br />
然而，董乐山生前并未享受过“国家补贴赔钱”的待遇。他翻译的一系列书籍，即使今天看来再冷僻，都既叫好又叫座，从来没让出版社赔过钱。<br />
原因很简单：“文革”后的中国，正是对知识最渴求的时刻，图书市场却几近空白状态，既没有畅销书、成功学，也没有股票证券、网络文学。勿论《1984》这样充满神秘感的小说，就是厚重的大部头历史著作《第三帝国的兴亡》，都能在公开发行后接连再版，畅销热卖。<br />
与董乐山的名字有关的书，真正让出版社赔钱的只有一套：他自己的文集。<br />
《董乐山文集》出版于2001年，收集了从他四十年代以“麦耶”为笔名写的那些回忆、剧评、小说，一直到晚年写的杂文、随笔、时评，足有140多万字。这套可称纯纪念性质的书只印了两三千套。通过李辉的牵线，河北一家出版社自愿赔钱出书，又“悄悄”把价格定为128元，以图尽量减少经济损失。<br />
许多大图书馆收藏了这套外观朴素的文集，借阅的人却很少。<br />
不管怎么说，这位一生追求理想的老人，死后终于享受了一次超脱现实的待遇。</div>

							
							
							
							
