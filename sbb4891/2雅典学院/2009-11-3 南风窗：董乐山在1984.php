最后见面竟是不欢而散<br />
<br />
一九九七年四月，我在香港《开放》杂志发表了一篇讨论名记者李却・伯恩斯坦新着《即将来临的中美冲突》的书评。我的结论是，中美或会冲突，但不会发生核子战争。五月我去北京看乐山，谈到此文，他对我的意见不以为然，厉声把我大骂一顿，斥我是帮中共讲话。而我只不过是发表读后感而已，预测未来核战不可能发生，不然全球毁灭，同归于尽，甚么善恶、甚么爱恨都没有了。对他的责骂我闷口不言。不料那次竟是我们最后一次会面。两年后，他因肝癌逝世。他的儿子亦波曾自美赶去送终，回来后我问他爸临终时有没有给我留言，他说没有，我听了非常伤心。<br />
<br />
《开放》杂志二○○一年八月号发表了主编金钟对我的访问记，我也提到我们兄弟最后不欢而散的一场。不料有人看到此文，在网络上发表文章把我骂得狗血淋头。他显然也曾受过中共折磨，但在思想上仍保留了中共气氛的熏陶，现在到了美国，他可自由发挥，他对中共嫉恶如仇，犹如乐山。他说他很敬重董乐山，我当然听了高兴；他指我所发表的文章持有「许多左倾甚至『亲共』观点」，不值我弟弟的敬慕。由于他在文中作人身攻击，我不屑作答。但我认为这类「亲我者友，逆我者敌」的态度，乃是中共最暴虐时期「文革」所余下的遗毒。那位显然自己吃过苦头的人，在进入自由环境下竟不能去除如此骂人态度，令我可惜。<br />
<br />
乐山是个聪明人，某次我们讨论在思想与行为上，左右两派若走极端会产生的结果。我们同意，到了极端的终点，共产党与法西斯思想相遇，都是一样的不容自由思想的发挥。这是真正的乐山，我的弟弟。<br />
二○○八年十二月九日于纽约<br />
