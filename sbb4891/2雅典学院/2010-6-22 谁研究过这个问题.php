																											<div id="postmessage_698147" class="t_msgfont">你们谁研究过这个问题 C 一个超越所有讨论的问题？<br />
<br />
中国之所以不能实行民主，也还没有找到从独裁到崩盘这样的循环，原因可以从吴思先生的隐蔽的秩序一书的375页找到答案。<br />
<br />
在这儿，作者叙述了中国人长久以来保有的观念。这个观念，哪怕是民主制度，也还没有办法根除。在没有解决这个中国人喜欢贪小便宜问题之前，所谓的民主制度，不过是黑金制度，所谓的人民监督，不过是打击报复。只有真正的解决了这个问题，所谓的民主化，普世价值，才有研究的意义。<br />
<br />
问题是，你们哪个有解决的办法呢？---- 我挑战所有1984bbs.com的网友来寻找解决的办法。虽然吴思先生可能是说，信仰是解决的思路，但是在中国，谈信仰是很不得人心的事情，只有谈金钱，大家才会眼睛亮一亮。<br />
<br />
以下摘抄于吴思先生一书的375页。<br />
<br />
&gt;&gt;&gt;&gt;<br />
<br />
“昨天半夜，一个朋友给我打了一个电话，让我帮他出主意。他是一家宾馆的老板，他发现自己的员工普遍贪污，从领班到服务员结伙盗窃。本来给顾客的优惠，被她们自己装进腰包。本来是自己上门的客人，被她们说成是出租车司机送来的，按照规矩给那些司机的提成也进了她们自己的腰包。这位老板最近连续解雇了三个员工，一个是有上述行为被他发现的，一个是涉嫌盗窃顾客财物的，一个是负责买菜却贪污菜款的。他说他不知道该怎么好了：&quot;我也不能把她们都开了，都开了谁来干活呀？&quot;<br />
<br />
我的第一反应，就是他的工资给得太低。我说你虐待员工吧？他说他们的工资标准属于中上等，绝对不低，他对员工也彬彬有礼。我说你监督不严吧？他说规章制度和执行情况都很严格，但是你不可能整天盯着她。只要有一点空子她就贪污，而一点空子也没有是根本不可能的。我说你为什么不逐步换人呢？他说换过人，新来的还是这德行。这时我想起了西方经济学的经济人假定：人是理性自利的，人们都要追求个人利益的最大化。因此，只要贪污的风险不大，贪污就是他们的最佳策略。我说，那你就认账吧，她们贪污是很自然的事情，别大惊小怪的，这是你必须付出的成本，只要别太过分就行。你就把这笔钱当成管理费用吧。他想了一会，说，也只好如此了。<br />
<br />
话虽如此说，回头细想，总觉得有点不对劲。为什么这里一点道德因素也没有呢？我们面对的毕竟是有尊严和良心的活人呀。我问自己：如果我是那些员工，我会贪污么？我确信自己不会。我可能偷懒，但绝对不会贪污，连想都不会想。那么她们为什么与我不同呢？也许，她们觉得自己那么做很合理？譬如，按照马列主义的说法，老板是剥削者，她们是被剥削者，剥夺剥削者并没有什么不道德的等等。或者干脆连这种自我辩护都不需要，有便宜不占王八蛋？<br />
<br />
今天早上起来去上班，我买了一份刚出版的1999年7月9日的《南方周末》，在第十四版上看到了一篇文章：《&quot;黑&quot;木耳暗访报告》。一位名叫刘根林的山东好汉，一次吃了劣质黑木耳呕吐，便自费追踪这劣质黑木耳的由来。他在安徽涡阳的两个镇数十个村庄见到了大规模的假货加工。在河北大城县的某村庄看到家家户户用硫酸镁等有害物质加工黑木耳，目的是增加分量和以次充好。他在黑龙江的木耳产地也见到黑木耳掺杂掺假。如此大规模的假货害了中国顾客的健康，也害了俄罗斯顾客的健康，俄罗斯已经不从中国进口黑木耳了。我估计许多中国顾客以后也不会再买黑木耳了。<br />
<br />
这就是说，报复最终会落到整个黑木耳产业头上，大家的饭碗都要砸。为了自身的长远利益，最合理的抉择应该是停止造假。但是团体理性与个人理性在此发生了冲突。每个造假的个体都有理由在最终的报复来临之前，努力捞上最后一把。反正自己不捞别人也要捞，这个产业早晚是要完蛋的。这种个人想法显然是合理的，于是这个产业就会真的完蛋。市场机制并不能摆平个人眼前利益和团体长远利益的冲突，负责这件事的应该是法律道德宗教之类的东西。在黑木耳产业里，我们显然看不到这类东西。<br />
<br />
刘根林看到的景象，家家户户公然伤天害理的景象，有一种惊心动魄的壮观。一个人偷偷干点伤天害理的事情，这在任何时代和任何地方都不希奇。一个村庄一片地区在光天化日之下如此大干，我们就很有理由怀疑，整个社会还有没有道德良心这种东西。这样的社会和人群在我看来是很希奇的。他们为什么这样干呢？他们如何为自己辩护呢？刘根林的文章里没有说。我猜想，自我辩护之类的需要恐怕只是我的需要，未必是他们的需要。他们大概也是信奉&quot;有便宜不占王八蛋&quot;的人。不过，如果一定需要找一个辩护理由的话，我也能替他们想到，这就是&quot;只许州官放火，不许百姓点灯&quot;。满世界都是贪官污吏，倚权仗势巧取豪夺，老百姓弄点假货赚点钱养家糊口，总比戴着大盖帽明抢道德多了。<br />
<br />
如果我迁居到这样的村庄，我能拿出什么样的理由来劝阻呢？为人民服务？笑话。毫不利己专门利人？神经病。&quot;五讲四美三热爱&quot;、争当&quot;四有新人&quot;？大傻帽。天理良心，损阴德折阳寿，伤天害理，不得好死，断子绝孙，下十八层地狱，除了这些古人的话，我想不出什么更有力量的说法。我好像出了毛病，或者是我们现在的意识形态出了毛病，很容易就能找到替损人利己的行为辩护的理由，但是却找不到有力的反对理由。天理良心，这是宋明理学的东西，被几百年来的英雄好汉斥为假道学的东西。损阴德折阳寿下地狱，这是迷信，传媒们正在起劲地反对着。反对了，打倒了，然后呢？光天化日之下还剩下什么？”</div>

							
							
							
							
