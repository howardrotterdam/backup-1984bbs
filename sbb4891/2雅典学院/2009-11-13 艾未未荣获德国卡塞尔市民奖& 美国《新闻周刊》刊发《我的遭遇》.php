<br />
《卡塞尔领奖信》<br />
<br />
亲爱的艾未未先生：<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;我非常高兴的告知您，您被选中并获得了2010年度卡塞尔市民奖“The Prism of Reason”(理性的棱镜)。 您获此奖项不仅是对您作为当代艺术家所做工作的承认，同时也为了您对自由的追求，对当局管理不善和贪污腐败的调查，对文化政策和民主方面的努力以及对歧视贫困人群的揭露。<br />
