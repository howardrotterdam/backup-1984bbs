																											<div id="postmessage_325921" class="t_msgfont">《我的遭遇》<br />
&nbsp; &nbsp; 艺术家解释为什么奥巴马的首次访华应该重申人权问题<br />
<br />
&nbsp; &nbsp; 八月十二日凌晨，我正在四川的一家宾馆内睡觉，猛烈的砸门声把我吵醒。大约有三十多个警察闯入了我的房间并且动手推搡。当我要求看他们的证件并发生争执时，他们动手打了我。他们扭住我的手臂后，猛击我的头部。一个月后的颅内出血几乎让我失去生命。<br />
 <br />
&nbsp; &nbsp; 我去四川是作为谭作人诉讼的证人，他被指控颠覆中国的国家政权。谭一直在试图确认2008年四川大地震中5000多名遇难孩子的名字。我为他的案子作证是由于我也一直在关注同一灾难。三月份时，由于政府一直拒绝给我们任何关于这些孩子的信息，我在博客上召集志愿者加入公民调查。从一开始我就说：“生命有它自己的尊严，你们不能只给我们个数字。他们的名字是什么？他们的双亲是谁？”志愿者们打了200多个电话到四川政府部门询问。那里的政府工作人员告诉我们这些信息是国家机密。尽管如此，我们还是在审查机关把我博客关闭之前发表了5000多名孩子的名字。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;在中国，政府封锁信息是有很长历史的，所以人民很难或永远不能了解真相。披露这个真相是生死攸关的。与大多数压迫人的社会相似，中国没有一个独立的司法系统。当一个证人被警察阻挠出庭时，这意味着我们的司法机构有如黑社会。并且没有独立的媒体去提出问题。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;本周奥巴马总统将就全球经济和气候变化问题首次访问中国。我很支持奥巴马，因为我相信他代表美国和世界的巨大希望，但是我无法理解他没有把人权放入访问中国的日程。当中国的公民没有起码的保护时，它的经济增长有什么意义？奥巴马必须在人的自由和尊严这一西方价值观上持有鲜明的态度。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;我没有能够出席谭作人的诉讼。警察打了我后把我拘禁在我的房间直到审判结束。（谭还在等待对他的宣判）一个月后我到慕尼黑举办我的作品个展。我称它“非常抱歉”，这是领袖们在灾难到来逃避责任时最喜欢的词汇。我用学童的书包拼成一个长幅覆盖了整个展览中心的正面。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;我被打之后一直感到的头痛加重，以至于不能集中精力。到医院检查时，我被告知脑正在出血，接近生命危险。我被紧急送入手术。当我醒来时感到几乎又是一个平常人了。但直到我与我的中国同胞们能够自由生活的一刻，我将永远不会感到完整。<br />
