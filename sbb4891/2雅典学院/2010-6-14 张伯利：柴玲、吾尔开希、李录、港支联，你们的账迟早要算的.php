<br />
但是，那都是由于在美国的压力下，那些国家的军队不支持政府的原因。但是这在中国绝对不会发 生。当有人把 刀架在空中，要砍你的脖子时， 去争论对方是否有道理砍你的脖子上是愚蠢的。当务 之急是把你的脖子移到安全 的地方去，等他刀放下来，再与他争论道理。当时柴玲是这么说 的：“现在那么多政府的官员一个个走马灯似地 劝告我们撤出，那是为他们着想，他们怕事情闹大 了丢官。他们怎么不为我们想想？如果全体学生撤出天安门广 场，李鹏就会轻而易举的把我们抓起 来。就是不抓起来，我们有好果子吃吗？我们与李鹏已经誓不两立，不是鱼 死就是网破。“<br />
<br />
柴玲的这番发言很清楚地表明她是用天安门广场上的广大学生掩护自己。柴玲是用几千个脖子去掩 护她一个脖 子。有人说６４学生领袖幼稚，听听他们的讲话，幼稚吗？<br />
<br />
