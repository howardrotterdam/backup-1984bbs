6月１日和２日，柴玲，吾尔开希，李录等６４头头对如何阻止戒严部队进入天安门广场作了严密布 置。还指定 专人负责把守一些街口。这些被指定负责把守的学生们都感到很光荣，他们很有一股为 革命流血牺牲的精神。但 是他们不知道的是一个巨大的阴谋开始了。89年６月２日深晚到３日凌 晨，北京高自联开了最后的常委会。会上 由吾尔开希重点发言。吾尔开希对大家说：”根据可靠情 报，明天戒严部队要进行武装清场，一定会发生流血死 人的。”吾尔开希又说“这次运动已经失 败。看来我们需要的不是一场改革，我们需要的是一场革命，一场体制外 的革命。我们今后所要作的是打倒共产党的体制。”柴玲，李录也作了慷慨的发言。 李禄接下来宣布高自联指挥 部成员立即 撤出天安门广场。再一次发了逃命钱。逃亡的方向是美国。吾尔开希对逃亡路径及联络方法作了细 述。先南下广州，然后由港支联接应。于是在６月３日凌晨三点左右，柴玲和大部分高自联指挥部 成员乘着天安 门上的学生正在睡梦之时，偷偷地撤离广场，走上逃亡美国的不归路。一个姓郭的头 头在临跑前，突然良心发 现。他说：“学生都没撤，我们指挥部提前撤对吗？是不是可以叫学生们 也一起撤？”李禄说“不行！叫学生们也一 起撤我们就撤不了”柴玲说“我们提前撤是为了保护火种”于是这最后的良心也被狗吃了。这时港支联的阴谋就完全 清楚了。先由柴玲，吾尔开希，李录 等人用豪言壮语把上千的学生骗在天安门广场，然后在武装清场的之前，柴 玲，吾尔开希，李录等 人再来个金蝉脱壳之计，逃之夭夭，使上千的学生陷入生死的困境。他们已经宣誓要打倒 共产党所 以，广场上学生死得越多，对共产党的打击越大，他们逃亡美国的本钱业越多。<br />
