<br />
在中国的官僚制度下，研究常常是推托的代名词，所谓研究就是研而不究。另一个更重要的事实是 万里说了”为 了防止意外，建议学生还是先撤出的好“他要留学生代表转告。北美中国留学生联谊 会立即把与万里谈话的详情 FAX给北京高自联。但是谈话一到北京全走了样。<br />
<br />
高自联的头头首先把万里说的”为了防止意外，建议学生还是先撤出的好“的话删除了。剩下的话 又变成了“人大委 员长万里将回国主持人大紧急常委会，取消戒严令”，这条消息一广播，天安门 广场上学生一片欢乐，大家沉浸 在一种虚假的胜利之中。万里在５月３１日回国，到上海时就下了 飞机并发表了支持戒严法的声明。天安门广场 上学生情绪先是失望，继而一下子又愤怒到极点，骂 万里变了立场。<br />
<br />
如果说柴玲，吾尔开希，李录推翻了全体学生撤出天安门广场的决定是寄期望于万里取消戒严法， 那么为什么在 万里声明支持戒严法后还坚持留在广场上呢？难道他们不怕死吗？柴玲在６４前的一 次接见美国电视新闻的采访 节目中有非常精彩的表现。我们看到节目播出是在大约８９年６月７日 左右。节目是专题介绍学生领袖柴玲的。 其中有一段对话如下。美国记者问：现在已经戒严了，你 们继续留在天安门广场上有没有危险呢？柴玲答：是 的。我们有很大的危险。我很难过，我们的这 些年青的学生们可能会为这场运动流血死去。说到这里。柴玲居然 哭了起来。美国记者又问：那么 你想不想死呢？柴玲一遍哭一遍答：不！我要话，我还很年轻。<br />
