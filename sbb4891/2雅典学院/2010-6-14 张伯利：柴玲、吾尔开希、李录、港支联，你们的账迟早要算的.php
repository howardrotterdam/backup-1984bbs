<br />
1989年，中国的经济改革正面临物价改革的瓶颈口，通货膨胀，物价上涨很快，老百姓情绪不满，而在这同时， 许多高干子弟利用经济改革的机会，搞 官倒，很快就把国家和人民的财富聚集在他们手中，这更加激起老百姓对 政 府的不满。所以当柴玲和王丹为首的学生，在天安门广场绝食反对贪污，反 对官倒时，很快得到北京市民的支 持。而且事态迅速扩大，并得到留美几万 留学生坚决支持。但是在后来运动就逐渐的变了性，柴玲，吾尔开希与 李录 的人性 的阴暗和丑陋的一面也逐渐暴露出来。<br />
<br />
在第一次与当时的李鹏总理谈判时，李鹏答应政府调查官倒和贪污。但是以 柴玲为首的学生代表拒绝撤出天安 门。柴玲说。撤出天安门广场后，李鹏会 对学生代表秋后算帐。在第二次与当时的李鹏总理谈判时，学生代表增 加了 不许对学生秋后算帐的条件李鹏也答应了。<br />
<br />
可是但是以柴玲为首的学生代表仍然拒绝撤出天安门广场。柴玲说我们不相 信李鹏。当然，李鹏作为中国太子党 的总代表，很难相信他会认真调查官倒 和贪污。可是继续占领天安门广场就会给李鹏以破坏社会秩序的名义把这 场 反对贪污，反对官倒的运动打下去。柴玲，吾尔开希与李录难道不明白这一 点吗？他们的智商就那么低吗？为 什么赵紫阳的幕僚一个一个走马灯似地劝 告他们撤出天安门广场，他们也坚决拒绝呢？他们一点也不幼稚，他们 是中 国近代历史上最精明的学生领袖。<br />
<br />
香港支联会从这场运动一开始就介入了。学生募捐的钱９０％来自香 港支联会。香港支联会驻京代表成了学生领 袖的参谋。是不是由于港支联的意思， 柴玲，吾尔开希等才拒绝撤出天安门广场呢？有迹象表明这一点。<br />
请看柴玲，吾尔开希等人的第一次逃跑：１９８９年５月１９日晚上，李鹏 宣布北京戒严。柴玲，吾尔开希等高 自联的头头吓得一人发了一千元，赶紧 逃命。当时柴玲宣布绝食团的使命已经结束。要大家赶快疏散。他们把这 一 千元叫作保命费。所以５月２０日和５月２１日，柴玲，吾尔开希，李录， 刘刚等许多高自联的头头都离开了 天安门广场，但也不在家里。可是在５月 ２２日，他们又回到了天安门广场。这两天，他们在那里呢？怎么又不 逃跑 呢？另一个敏感的问题是：他们当初准备往那里逃跑呢？<br />
<br />
有一个高自联常委的活漏了天机：他骂道“香港支联会真不是东西，说是支持我们，现在捅了漏 子，就不管我们 了。”在５月２０日和５月２１日这两天里，有些高自联头头要求港支联协助他们 逃亡美国，但是港支联不肯答 应。为什么不肯答应？港支联说，仅仅戒严令还不足以说明你们有危 险。换句话说，还没死人呢！<br />
<br />
鉴于柴玲的逃跑，她被撤除高自联总指挥的职务，由王丹任总指挥。５月２４日在王丹的主持下， 高自联常委会 通过了定于５月３０日全体学生撤出天安门广场的决定，并且通知了中国留美学生联 谊会和香港支联会，要他们 在５月３０日前游行一次。（中国留美学生就于５月２８日，在纽约， 华盛顿，芝加哥，洛杉矶等地举行了大游 行。）港支联眼见北京学生运动越出了它的操纵，就派特 派员紧急飞北京。他与柴玲，吾尔开希，李录等秘密会 谈，作了一场丑恶的交易。首先，由柴玲发 难，猛烈攻击王丹是右倾分子，投降主义。<br />
<br />
经过高自联常委会的紧急会议，推翻了原定于５月３０日全体学生撤出天安门广场的决定，同时撤 除了王丹的 职，由柴玲重新担任高自联总指挥。此外，柴玲又在学生运动的口号中加上了“要李鹏 下台”的一条。这就是使学 生运动打上了死结。人们要问为什么柴玲，吾尔开希，李录有这么大的 能量呢？一是由于，他们控制了募捐来的 钱，二就是他们与港支联特别关系。一些人支持柴玲就是 想一举跳向美国。王丹当时说”中国的民主需要长期斗 争，不是一天两天能成功的“王丹当时还说 过“这次运动的目的已经基本达到，反官倒和反贪污口号已经深入人 心，<br />
