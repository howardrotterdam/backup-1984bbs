													<h2>张伯笠:柴玲信主了！</h2>
						
						
																											<div id="postmessage_682547" class="t_msgfont">昨天，柴玲打来电话，兴奋地和我分享她从信主到受洗的每一个细节，在她快速的叙述中找几乎无法插话，但我能感到她从内心涌流出来的喜乐，我感觉到二十年前我在北大校园认识的那个一头短发快乐天真的小丫头又回来了。<br />
<br />
 <br />
<br />
二十年前天安门的屠杀改变了我们“天安门一代人”一生的命运，而作为天安门学生运动总指挥的柴玲无疑首当其冲。<br />
<br />
<br />
她在中国曾艰难地逃亡了十个月的r间，据说中国沿诲城市曾在那段r间抓到了几十个“柴玲”。逃亡对一个二十四岁的女孩子并不轻松, 那不是电影中的惊险片断，而是真实的危险和死亡的威胁。<br />
<br />
 <br />
<br />
她逃到美国后首先经历了离婚的痛苦，那是她和我们都不愿意碰的痛，因为我与男女双方都是“战友”。他们离婚不久后我逃到海外，在巴黎的一次相聚中她告诉我她常常想不活了，一个人走不下去了，她内心的痛苦我无法测度。只记得那次的相见整整一盒纸巾都未擦干她的泪水。<br />
 <br />
<br />
 <br />
<br />
 2、6月4日早晨从天安门广场最后撤出学生，讲演者为张伯笠，身后为柴玲、封从德、李录等。<br />
<br />
离婚不久她受到更大的打击，她做军医的母H和她深爱的祖母在痛苦的思念中相继去世，记得她母H去世那天是柴玲的生日，我从普林斯顿医院逃出来去看她，她哭著说：妈妈二十五岁生了我，而当我二十五岁生日r她却离开了我，都怪我……那晚突然停电了，她点燃了一只红蜡烛，我们在烛光下泪水中默默地伴她遥祭自己的母H。对于我们这些天安门一代，哪一个人不对家和亲人亏欠和内疚？很多人的H人失去了地位和工作，甚至失去了生命，这重担足够我们担一生的。<br />
<br />
 <br />
<br />
这还不够，上世纪九十年代中期， 由一个纪录片精心编造的柴玲期待流血，自己逃生的假象引导公众指责，使柴玲心灵受到至命的伤害。海外最大的中、英文报纸开始长篇累牍地讨伐柴玲，把屠杀和死亡的责任推给了柴玲，甚至有些参加了天安门运动的精英也在落井下石，弱小的柴玲一r无法招架，痛苦万分。<br />
<br />
 <br />
<br />
我是当时少有的几个站出来为她讲话的人，我认为那种屠杀的责任在学生一方的说法就象是指责一个被奸杀的少女一样：谁叫她反抗了？没看到强奸者手中有刀吗？听话不就没事了吗？…… 这些强盗的理论竞然被视为正常，好象世界历史上只有中国的精英站在政治操作的层面上指责被污辱被屠杀的人，这是中国知识分子的悲哀，因为他们思想中只有利益]有正义。这些人不象人！在金钱和权力的诱惑面前中国知识精英几乎集体犬儒，集体出卖了良知，直到有一个叫邓月娇的女孩子向这种理论说“不”。<br />
<br />
 <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;其实，在屠杀面前，柴玲和所有广场的同学一样都有求生的愿望和权利，都有孝敬父母恋爱结婚养育子女的梦想，这是人之常情，并没什么错。耶稣在上十字架前也说过： “父啊，让若愿意，就把这杯撤去，然而，不要成就我的意思，只要成就你的意思。”(路加福音22：42)耶稣说的“杯”，指的是十字架上的苦难和死亡， 何况柴玲是人不是神，她只是一个23岁的年轻女孩子。我们为什麽要痛斥柴玲的语言哪？<br />
<br />
 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; 今天信主后的柴玲虽然曾经备受煎熬，但她选择了饶恕，她认为这是人们不了解历史真相的原因, 象基督在临行前的另一句话一样， “父啊，赦免他们， 因为他们所做的是他们不知道的。” 但是我们不能继续容忍有意误导。 试想一下, 如果我们只读到圣经里面耶稣先前的挣扎, 没读到耶稣胜过试探甘愿顺服，最后毅然走上十字架，用他自己的受难和死亡去为我们赎罪，完成上帝的旨意，给他挚爱的人类一个永生的机会，那我们对耶稣的认识会是多么的片面。<br />
<br />
 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;事实上， 在5月28号上午柴玲说过那些求生的话后，她下午就放弃了离开广场去劝阻军队的计划，从此始终留在广场， 直到6月4号清晨，柴玲跟天安门广场最后的几千名学生一起，自愿地放弃了她求生的愿望，放弃了她对挚爱的的父母、年迈的奶奶、幼小的弟妹和年轻的丈夫的眷恋，坦然和坚定地在广场的纪念碑下面对机枪和坦克， 准备为热爱的祖国和陌生的同胞的新生和自由献身。 她从没有丢下同学们逃跑，更没有逃进外国大使馆或几天前就逃到了东南沿海等待偷渡。她一直坚持在广场直到六月三号晚上面对机枪坦克， 在六四清晨多数同学做出撤离的决定， 她才带领同学们在枪口和军队包围中走出广场，而且她走在第一排，那是最危险的一排，如果军人开枪，她会第一个死亡, 这需要了不起的勇气和奉献精神， 这是柴玲和天安门一代对祖国奉献的最大的爱！<br />
<br />
 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 柴玲曾和我说过她活著的沉重, 我们对死难者永远心存愧疚, 尤其是备受磨难的天安门母亲们。这也是她这么多年来一直被深压在生存罪过感的苦海里的原因。作为一个目击者和幸存者， 我知道，6月4日清晨，在硝烟和弹屑中，我们一起最后走出天安门场，走到北京大学，在我们后面的是肆虐追逐的的坦克， 又夺取了十几个同学的生命和身体。 柴玲直到把活下来的同学全部带回学校， 总指挥的任务结束以后， 才开始了走向了她个人后来才知道的在中国大江南北， 艰辛逃亡的十个月生涯。 直到在最后一刻， 她被关在一个船的木箱里4天5夜才死里逃生， 历尽艰险逃出生天。<br />
<br />
 <br />
<br />
所以我认为，她能如此神奇地活下来，只能归功于上帝的恩典。<br />
<br />
 <br />
<br />
面对对柴玲的漫骂与指责, 作为七尺男儿，我不忍心让一个在那场运动中失去了家庭失去了母亲奶奶的弱小的女孩儿单独背天安门屠杀的十字架！我只所以站出来说真话，不为别的，只求良心的平静。<br />
<br />
 <br />
<br />
柴玲没有邓月娇的修脚刀，她选择了沉默，从那以后，那个小鸟般快乐的小丫头不见了，柴玲从媒体和人们视线中消失了，她不再与任何人联系，也不再相信任何朋友和战友，她将自已裹的严严的，恐怕再一次被所谓的“战友”和 “自由媒体”伤害。连我也十几年未见到她。<br />
<br />
3、作者在新加坡布道<br />
 <br />
<br />
当世人把她当做渣滓r，神却高举了她。后来我知道，在那段孤独、失落的日子里，神从未撇下她。神将一位爱主的美国弟兄带到她的身边，他们相爱了，结婚了，生了三个可爱的孩子。后来又办了个软件公司，很蒙上帝的祝福。而我也神学院毕业，成了传道人，思念她r，也不知电话号码，只有常常为她祷告。<br />
<br />
 <br />
<br />
去年在华盛顿六四二十周年对死难者的祈祷会上，我看见了柴玲，在《奇异恩典》的歌声中她泪流满面，我从台上走下来和她握手拥抱，那天我按手为她祷告，封锁和其他的几位成为了基督徒的同学们也一起按手在她身上， 祈求上帝来引导柴玲的灵魂。那一刻，圣灵感动我，终有一天她会再被我们心怀无边的神找到，和我们站在一起，成为上帝的儿女。我相信，无论这个世界有多少患难、困苦、逼迫、孤独、冷漠，都不能使我们与神的爱隔绝。我相信有一天，我们会在基督的国度中聚集！<br />
<br />
 <br />
<br />
正象使徒保罗所说的那样：“谁能使我们与基督的爱隔绝呢。难道是患难麽，是困苦麽，是逼迫麽，是饥饿麽，是赤身露体麽，是危险麽，是刀剑麽。…… 然而靠着我们的主，在这一切的事上，已经得胜有馀了。” (罗马书8:35-37)<br />
<br />
 <br />
<br />
那次祷告会是2009年6月4日夜晚，整整六个月过后的2009年12月4日，柴玲的心灵被主的慈悲和恩典感动，决志接受耶稣为主，成为主的女儿。 感谢主，神听了我们的祷告。哈利路亚！<br />
<br />
<br />
 4、许多人归向耶稣，作主门徒。<br />
 <br />
<br />
昨天，柴玲传来她的见证，那是她为受洗和德州一次中美基督徒聚会中准备的，她当r使用的是英文稿，后由德州的弟兄姐妹译成中文。她告诉我她在刚做完一个牙龈手术后， 忍痛一鼓作气， 靠神的启示，用了六个小时就写完了这个见证。我今天将这个见让推荐给弟兄姐妹，让我们为她祷告，让神的爱除去她过去生活中一切的伤害，让神的灵引导她一生爱主、跟随主、事奉主，荣耀主！<br />
<br />
 <br />
<br />
“若有人在基督里，他就是一个新造的人，旧事已过，都变成新的了。”<br />
<br />
 <br />
<br />
