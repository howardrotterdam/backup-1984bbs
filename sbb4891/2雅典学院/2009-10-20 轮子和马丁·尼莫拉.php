																											<div id="postmessage_291889" class="t_msgfont">1、从宗教信仰角度，我排除轮子。我把宗教信仰分为基督教、佛教、伊斯兰教三大类，以及众多小教派。一定要说轮子，只能归于小教派。至于邪教，一般都在小教派之列，共产邪教除外。轮子从目前表现看，不属于邪教。大陆宣传的轮子的“邪教”表现，无法证实，却很可疑是被栽赃，并可部分证实。<br />
<br />
2、轮子的兴起，背景是改革初期众多气功。当时特异功能是得到当局鼓励和赞赏的，特异功能和气功相近，我归于气功。而轮子的初衷，也和气功相近，骗人钱财而已。而之所以能够骗人钱财，是由于社会医疗的不足。有病不用吃药不用花钱，是当时轮子引得广大民众趋附的重要原因。<br />
<br />
3、《转法轮》是否李大师所著，十分可疑。其内容，使用了大量科学新概念，或说新名词，但无一不是牵强附会，甚至狗屁不通。这种使用大量牵强附会的科学新概念新名词，甚至独创一些模糊概念名词的做法，现在被使用在大量医药或保健广告上，当局视而不见。而这种手法，恰恰是迷惑许多人的地方。<br />
<br />
4、从《转法轮》的出台，到轮子在海外的兴起，显然有背后某种力量的支持。我不怀疑如大纪元等得到很多义工，但仅有再多的义工而没有真正有实力的支持，不可能发展到如今的规模。<br />
<br />
5、轮子的所有作为里，如自由门之类的翻墙工具，是对尤其是铁幕下人们的最大贡献。这点不容置疑，也应该得到肯定和尊敬。<br />
<br />
6、从99年开始，当局对轮子实行大规模镇压，其手段令人发指。无论是自导自演的天安门自焚，还是器官移植，都有足够的事实可以证实。<br />
<br />
7、大陆内外，所有追求自由民主的人们，在相当的一段时间内，对轮子的遭遇闭口不言。这毫无疑问是耻辱。近年来这种情况有所改变，首举之人为高智晟。无论高本人如何，动机如何，之后的变化如何，在这点上的贡献也不容无视。<br />
<br />
8、任何潮流，站在潮头上的，往往并不是人格完美的人。但善良的人们切记：千万不要因这些不完美的弄潮儿而否定整个潮流。大浪淘沙，风雨过后，另是一番天地。此为本人文革所得的重要体会，对六四亦如此。<br />
