　　奥巴马这一次破例为南方周末长脸，相信是为南方周末以往鞍前马后传播美国版普世价值的一种肯定，也是对其今后大放厥词信口雌黄提供切实的“战略再保证”。有人再敢对南方周末怎么样，那就成了对美国总统的不敬，成了两国关系问题了。<br />
　　<br />
　　南方周末，至少是它的评论，相当程度上，庄严地代表着美国的利益。过去，人们不理解，为什么南方周末不遗余力地传播美国的价值，并将其易名为普世价值（美国国内极少有人使用这个概念），将中国改革开放称作向普世价值及看齐的过程，将中国人抗震救灾的努力，视作兑现普世价值承诺的过程，将对执政党的最高褒奖歪曲为是否践行了美国人的普世价值……过去，人们更不理解，为什么南方周末不间断地以各种形式咒诋毁毛泽东同志，诋毁以毛泽东同志为核心的老一辈革命家为创建新中国建设新中国所建立的丰功伟绩，将建国的前30年与后30年人为地对立起来……今天，这一切有了答案，我们应该感谢奥巴马，感谢南方周末。毛主席向来喜欢从逆向思维的角度，重视反面教员的作用，今天，我们这些后来者也应该有这样的胸怀、境界和智慧。<br />
