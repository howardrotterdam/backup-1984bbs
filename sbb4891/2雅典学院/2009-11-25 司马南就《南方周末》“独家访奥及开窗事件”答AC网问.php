　　司马南:具体细节不了解，无从评论。我只能说，南方周末同仁今天起点很高，方励之李淑贤夫妇不具可比性。方李已经是骨灰级人物，南方周末搞事者正值年少，此事件对中国新闻事业发展，对社会和谐有序的建构，对中美关系影响深远。处理好这件事，需要务实灵活的处事风格，更需要“国家利益第一”的政治信念。<br />
　　<br />
　　问：南方周末与奥巴马到底谈了什么？南方周末的人故作神秘偏偏不讲，但是，司马老师您看了吗，一些人的个人博客，他们掩抑不住眉梢喜悦，却又作出一幅受了大委屈的样子，事情相当吊诡……<br />
　　<br />
　　司马南：我看了一些国内外对此事的报道，周边的好事之徒、遥远的大纪元同好，均不断造谣谎称奥巴马接受南方周末访问言及民主、人权、新闻自由等为中国当局所不容。可是从已经公开的那几段采访记录看，干巴巴地没有任何新意。我们身在国内，当然清楚地知道他们造谣，奥巴马甭说他讲民主自由，他爱讲什么讲什么，上次小布什总统来，不是大讲一通基督教吗，CCTV照例直播。<br />
　　<br />
　　我个人浅显的看法是，奥巴马与南方周末没有、也不需要谈什么敏感和保密的内容。独访美国总统，美国总统声援称赞南方周末的新闻效应已经足够了――它本身就在传递着独特的信息――只有南方周末才是美国当局真正喜欢的报纸，奥巴马与南方周末要的是同一个东西：瞧一瞧，看一看啊，我们才是真正的“心心相印”的“面对共同挑战的伙伴关系”。<br />
