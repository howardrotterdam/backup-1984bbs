　　析南方周末的走势，眼下，已经获得了“战略再保证”框架之内“美国租媒”（美国租界媒体）的特殊的政治地位，“新自由主义的大本营”已经远远不能概括南方周末的特质，其必然会以曲折委婉的花样翻新的手法，一如奥巴马个人的演讲风格一样，进一步挤压蚕食中国的舆论阵地，“呛声”（台湾人熟悉的词）中央，拿出一连串新的动作来。<br />
　　<br />
　　――――――――――――<br />
　　<br />
　　问：从安全角度说，美国总统到中国访问，这是一件天大的事，保卫总统的安全工作，无论中方还是美方都会力求做到铁壁铜墙天衣无缝，怎么能搞出本来不应该出现在采访地点的人，竟然越过安全部门道道封锁，乘着美国使馆的车来觐见美国总统呢？是中国方面的情治人员严重失误，还是美国谍报机关配合？<br />
　　<br />
　　司马南：这个问题不应该问我，应该问总统先生和美国中情局。他们这种事情从来不知会司马南。<br />
