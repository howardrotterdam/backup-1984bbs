																											<div id="postmessage_802557" class="t_msgfont">　　1957年11月，以苏联为首的社会主义阵营，在莫斯科开会，会议签署了《莫斯科宣言》，各社会主义国家形成共识，“社会主义正在上升，资本主义正在衰退”。当时，苏联刚刚发射了第一颗人造卫星，赫鲁晓夫在会上提出，要用十五年时间赶上和超过美国。作为苏联的小弟弟，毛泽东代表中国拍胸脯，中国要用15年时间赶上和超过英国。12月份，刘少奇向全国宣布，中国要在15年内在钢铁和重工业方面赶上英国。<br />
<br />
　　吹牛的基本规则，是越吹越大，越吹越没谱。毛泽东在莫斯科表示，赶上英国需要15年；回到国内，这个时间就在不断缩短，先是“内部要求”，用七年赶上英国，八或十年赶上美国；紧接着，大跃进在全国全面展开，毛泽东认为，人民公社是中国贡献给国际共产主义运动的样板，苏联的“苏维埃加电气化”已经落后，中国很可能比苏联提前进入共产主义，只需三五年就可以赶上英美。<br />
<br />
　　赶英超美的重要条件，就是钢铁产量，赶英超美的第一战场，也在钢铁。英国当时年产钢量约为2000万吨，中国1957年的年产量为535万吨，为了加快赶超的步伐，中国决定1958年的年产量为1070万吨DDD依照如此速度，不需三五年，只需两年，就可以赶上英国了。“一万年太久，只争朝夕”，于此可见。所以，赶英超美，最高昂的乐章，表示在“大炼钢铁”领域。比如湖北大冶钢铁工人所创作的诗歌：“红钢好似火龙翻，围着圆盘两边蹿。银盔金甲如闪电，超过英国老约翰。”<br />
<br />
　　有意思的是河南洛阳人民的诗歌：“炼铁要登云梦山，云梦山上把家安。白云深处搭帐篷，铁炉建在高山巅，拉起风箱火焰高，炼出铁水像山泉。别看俺的锅炉小，产品堆起撑破天，铁水滚滚似火龙，能把地球缠三圈。英帝看见心发慌，美帝气得干瞪眼。”诗中嵌入“云梦”二字，固然为实景，却隐含着“痴人说梦”的谶语。<br />
