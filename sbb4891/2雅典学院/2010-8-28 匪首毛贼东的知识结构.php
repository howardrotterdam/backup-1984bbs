																											<div id="postmessage_801567" class="t_msgfont">（接上文）<br />
历史书既然“较柔和”，在他的理解范围内，那应该起码把国际共运史、十月革命史、联共党史等等仔细看过来吧？否则怎么去“继承捍卫发展”列宁主义？然而他就有本事闹出这种笑话来： <br />
<br />
“要快之事，马克思也犯过不少错误。我搬出马克思来，使同志们得到一点安慰。这个马克思，天天想革命快，一见形势来了就说欧洲革命来了，无产阶级革命来了，后头又没有来，过一阵又说要来，又没有来。总之，反反复复。马克思死了好多年，列宁时代才来。那还不是急性病？‘小资产阶级狂热性“？马克思也有啊！（刘少奇插话：列宁也有，讲世界革命很快就要来了。）世界革命，那个时候他希望世界革命来援助，他也搞和平民主新阶段，后头不行了，搞出一个一国可以建成社会主义，这在以前也讲过吧？（刘：是一国可以胜利，一国可以建成社会主义没有讲。）一国可以胜利，到这个时候，不建怎么办？只有一国。（刘：依靠自己本国的农民可以建成社会主义。）依靠农民。巴黎公社起义之前，马克思反对。季诺维也夫反对十月革命，这两者是不是一样？季诺维也夫后来开除党籍，杀了头。” <br />
<br />
他这儿披露的马克思多次预言欧洲无产阶级革命到来的独家消息，本人无学，不知道是否属实，只记得马克思强调过，只要资本主义生产关系还有容许生产力发展的余地，无产阶级革命都不会爆发。至于毛披露的苏俄革命史则完全是笑话。列宁一开头确实想和平夺权，鼓吹“一切权力归苏维埃”，但那并不是“希望世界革命来援助”，而是因为党内大多数人都主张与孟什维克合作。哪怕在他提出准备武装夺权后，多数党魁仍然认为社会主义革命条件不成熟，支持他的只有托洛茨基。他确实是把胜利的希望寄托在世界革命上，以为只要俄国打响社会主义革命的第一枪，欧洲先进国家特别是德国的无产阶级就会跟上来。直到死那天，他都“希望世界革命来援助”，盖他认为“社会主义革命在一个国家取得完全胜利是无法设想的”，从未提出过“一国可以建成社会主义”。那是斯大林的发明，也是忠于列宁思想的托洛茨基与斯大林争吵的核心问题之一。毛这位“伟大的马列主义者”竟然连斯大林的贡献是什么，其与托洛茨基“路线斗争”的焦点是什么都两眼一抹黑，绝对是只有中国才会闹出来的笑话。 <br />
<br />
少奇同志也够呛，他毕竟去苏联留过学，大约恍惚听到过“一国可以建成社会主义”是斯大林而非列宁的主张，于是出来纠正老毛，说列宁说的是“一国可以胜利”，什么胜利？当然只能是社会主义革命胜利。姑不说上引列主席语录已经表明，列主席认定俄国社会主义革命胜利的前提是世界革命，不可能在一国单独取得胜利。就算列主席没这么说过，那请问“社会主义革命胜利”与“社会主义建成”究竟有何区别？莫非可以有社会主义革命业已取得胜利，而社会主义尚未建成的怪事？当然，如后文将要解释的，这是列宁和斯大林修正马克思主义造出来的悖论，须怪不得少奇同志。比起伟大领袖那马列盲来，他可算精通马列了。 <br />
<br />
由上可知，毛泽东的知识结构有着非常醒目的特点，那就是极度“偏科”──彻底缺乏西方传入的一切知识，而对旧文化包括显性文化（儒教经典、史籍等）与隐性文化（亦即游民文化经典、稗官小说等）则极度精通。这种极度畸形的知识结构，决定了他绝无可能理解建筑于对西方社会的剖析之上的马克思主义，但却丝毫不妨碍他掌握列宁、斯大林发明的革命工艺学。那并不是什么复杂抽象的理论，也不需要对西方社会和文化有任何了解，无非是如何建立党组织，利用它去发动、组织、控制群众，将民力化为夺权战争资源，并加以极限开发利用。上面已经说了，这套政治技术可以从马克思的理论框架中独立出来，不必非得学会马克思主义才能掌握，由苏联顾问和留苏学生们的言传身教即可。但它只是干革命的基本手段，并不能解决具体的战略战术问题，后者只能用中国传统的阴谋诡计去解决。 <br />
<br />
这就是毛泽东与与国际派“教条主义”者们最本质的区别。那些人虽然从莫斯科“背回来几大麻袋教条”，却不懂国产阴谋诡计。而毛的长处就在于他通过阅读史书、烂章回小说、烂报纸，对社会各阶层的人物心理和性格上的弱点极度熟悉，知道该怎么去最有效地利用它们。在掌握列宁发明的基本革命技术后，再加上对传统阴谋诡计的娴熟运用，当然要构成极大主观优势，使得毛在党内外权力斗争中胜出，最终赢得了中共革命胜利。这就是“把马列主义的普遍真理与中国革命的具体实践结合起来”的真谛，它说的其实是“把列宁发明的革命工艺学与中国原有的造反打天下、整人控制人的阴谋诡计结合起来”，从头到尾都与马克思主义委实没有什么相干，根本不是什么“马克思主义中国化”，而是毛的病态知识结构在病态社会中的歪打正着。 <br />
<br />
正因为毛的畸形知识结构在革命战争中帮了他的大忙，而国际派背回来的几大麻袋教条什么用处都没有，毛才养成了对西方文化的轻蔑心理，甚至数典忘祖，认为“马克思可用的东西不多了”。他的确是任弼时说的“狭隘经验主义者”，终生无法从自身的成功经验中超越出来，没有看到在成功夺取政权后，他面临的不是传统开国君主“坐江山”的快活岁月，而是全面改造中国，将中国建设足以领导世界革命的社会主义强国，为他解放全人类提供强大的物质基础。只有这才是仿效斯大林晋升为全世界无产阶级领袖的正道。而实现这一远大理想的秘方，从《精忠岳传》、《官场现形》、《子不语》、《兰花梦奇传》、《峨嵋剑侠传》、《小五义》、《水浒》、《薛刚反唐》、《儒林外史》、《何典》、《清史演义》、《洪秀全》、《侠义江湖》一类垃圾读物中是绝无可能找到的。 <br />
<br />
这就是他何以会在当国后无师自通地获得狗杂种石破天（参见金庸《侠客行》）那伤人于无意之间的毒掌神功，无论什么东西，碰上了都非死即伤。他非但悟不出，不懂西方专家治国那套，就绝无可能让中国成为现代强国，而且就连两千年前的贾谊都不如，始终没有看到“打天下”与“治天下”需要的是完全不同的智能──“并兼者高诈力，安危者贵顺权”，打天下需要的是“诈力”，而治天下则是顺势权变，“务在安之”。毛却以马上得之，以马上治之，正是“自矜功伐，奋其私智而师古”，终生以诈力经营天下，比项羽也高明不到哪儿去。 <br />
<br />
要练就这毒掌神功，除了知识结构残缺畸形外，还需要醒目的智力与性格缺陷。人的智力有各种定向。毛是非常罕见的造反天才，精通各种匪夷所思的整人控制人的阴谋诡计，却是更罕见的治国白痴，彻底缺乏建设性才能。这儿的“建设”是广义的，并不光指经济建设，而是包括建立思想体系、制定各种可运作的制度的智能。这就是毛最突出的特点，它是由一系列智力缺陷决定的。<br />
