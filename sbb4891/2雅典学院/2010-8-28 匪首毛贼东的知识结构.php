													<h2>匪首毛贼东的知识结构</h2>
						
						
																											<div id="postmessage_801566" class="t_msgfont">芦笛：毛泽东的知识结构和来源<br />
<br />
如所周知，毛泽东出生于前清，生长于当时信息闭塞、教育落后的湖南，没有受过什么正规良好的教育。他终生不懂外文，熟悉的除了游民文化经典和各种稗官野史之外，不外乎传统经典、宋明理学、王船山学说、《曾文正公家书》那一套，外加长沙图书馆能找到的寥寥几本翻译拙劣的西学书籍以及各种报纸，彻底缺乏科学常识、现代文明常识以及外国政治文化常识包括马克思主义常识。最严重的问题是，他有传统读书人的深重的井蛙情结，到死也未发现自己的知识残缺，对西方文化毫无兴趣，终生只看中国的书，认定那里面有解决一切难题的答案。就连长沙师范教的那点简单自然科学常识，他都毫无兴趣，门门不及格还引以为傲，在多年后向斯诺吹嘘，考平面几何时他在卷子上画了个圆，在下面题上“半壁见海日”。根据他的自述与旁人的介绍，不难勾勒出他的知识结构： <br />
<br />
　　1）现代（＝西方）文明常识（含自然科学常识）：彻底阙如。 <br />
<br />
　　2）各种劣等低质旧小说：这构成毛的主要学习内容。 <br />
<br />
　　3）民国时代各地出的烂报纸：也是毛的主要学习内容。 <br />
<br />
　　4）儒家和道家的经典：这是那个时代的人的必读书。 <br />
<br />
5）《二十四史》：这构成了他执政后的主要学习内容，当然不可能读全，他反复读的是《资治通鉴》。 <br />
<br />
6）介绍马列主义的二手小册子以及几篇马列的著作。主要是在延安时期看的，只限于哲学方面。59年庐山会议后后似乎看过苏联的《政治经济学教科书》。 <br />
<br />
他给毛岸英开的书单足可印证上述分析： <br />
<br />
“精忠岳传2，官场现形4，子不语正续，3，三国志4，高中外国史3，高中本国史2，中国经济地理1，大众哲学1，中国历史教程1，兰花梦奇传1，峨嵋剑侠传4，小五义6，续小五义6，聊斋志异4，水浒4，薛刚反唐1，儒林外史2，何典1，清史演义2，洪秀全2，侠义江湖 6。” <br />
<br />
现代人必然要纳闷：毛为何会对那种毫无文学价值、思想价值与社会价值的烂小说感兴趣，还要把它们当成太子的必读书？他当年在长沙创办“自修大学”，天天到长沙市图书馆去读书。那阵子的图书馆能有什么有价值的书？西方社会科学的作品都是后来才陆续翻译过来的。毛亲口承认，到延安之前，他只看过《共产党宣言》中译本，只取了四个字“阶级斗争”，对马列一无所知。看来他把功夫全花到攻读各种各样的劣等章回小说和报纸上去了。毛认为报纸里有很多学问，花了大量时间把报纸的每个字都看过来，倒与今日的网虫差不多。他在《西行漫记》中还强调过读报的重要。左舜生也曾说过此事，对毛这种雅兴大惑不解。凡受过教育的人恐怕也不能理解：看报能学到什么系统的学问？<br />
<br />
问题还在于，即使是在后来有了条件之后，甚至在他当了国、马列著作被大量翻译过来之后，毛仍然对外国人写的东西毫无兴趣。何方指出： <br />
<br />
【“毛大概没有读过《资本论》”，对政治经济学“没有下过大功夫”，“喜欢读古书， ……曾经认为 ‘马克思可用的东西不多了’， 说经典著作读起来没味道”。（李锐：《毛泽东的早年与晚年》，贵州人民出版社1992年版，第122，123页。有关毛泽东没有读过《资本论》的说法，长期以来流传就较为广泛。不久前（2002年11月22日）的一次会上，曾彦修还提到，田家英告诉他毛主席没读过《资本论》。于光远在旁加以证实说，他可算“权威”，因为他在延安时是图书馆主任。除人们传说外，毛在文章中也确未直接引证过《资本论》。《毛泽东的读书生活》一书中提到“1954年，毛泽东又一次阅读《资本论》”，似乎不足信。因为插图的《资本论》只是写了个出版年代，难以证明读过。毛读书有作批注的习惯，更何况读《资本论》，但人们没见到过批注。和插图中的版本极相似的一本（可能就是这本）曾在毛中南海故居展览过，我参观时翻了翻，未找到批注。现在公开出售的毛泽东读书批注极多，大部系古籍，但也有《毛泽东哲学批注集》和内部出的《毛泽东读社会主义政治经济学批注和谈话》（清样本）。如果《资本论》读了几遍（1954年是“又一次阅读”），那一定会有大量批注可供整理出版。而且像《资本论》这样大部头的书（加上《剩余价值理论》共六大本），毛恐怕不但没兴趣（“没味道”），而且也没时间读。在延安整风前学理论高潮期间，他钻研的是哲学，没参加《资本论》学习小组。此后大概更不容易抽出阅读的时间了。另据莫洛托夫回忆，毛向他承认，自己没读过《资本论》。见（苏）丘耶夫：《莫洛托夫访谈录》中译本，吉林人民出版社1992年版，第123页。） <br />
<br />
毛对中国古籍百读不厌，但对通行于世界的经济学、社会学、国际关系理论一类书却读的不多，对外国文学更没多大兴趣，很少读什么世界名著，据说只读了《毁灭》、《简¨爱》两本外国小说，《红与黑》还是只看电影没看原著。但他对中国古籍读得多，记忆力又好，许多古文、诗词，可以倒背如流。所以有人说，“他对中国典籍的熟悉程度远超过他对马克思主义的熟悉程度。” （李锐：《毛泽东的早年与晚年》，第307页。） 他在讲话中经常以古喻今，可以长篇引证原文，顺手拈来，毫不费力，但从未见他引证过马克思、恩格斯的大本原著上的话。有人统计，《毛泽东选集》中引用中国古籍比马列（又以斯大林为最多）要多出四倍。他不但自己爱好古籍，还经常挑选一些古文叫印发给中央领导或某个会议参加者看，如《后汉书》的《张鲁传》、枚乘的《七发》等。有时还要考问一些领导人，《资治通鉴》看了几遍？直到临死前还要人给他念庾信的《枯树赋》、贾谊的《□鸟赋》，以及岳飞、张元干等人的词（陈晋：《毛泽东之魂》，吉林人民出版社1993年版，第402-405页）。 】 <br />
<br />
其实毛自己就承认他不学有术，根本不懂马克思的基本理论。在1959年庐山会议上，他说： <br />
<br />
“华北座谈会后，也想搞点马克思主义。我的‘狭隘经验主义’称号，是任弼时加的；对我有很大帮助，读了几本书，很感谢他。德波林的《欧洲哲学史》，就是打水口期间读的。原来不懂辩证唯物主义、历史唯物主义是什么东西。两者就是马克思主义的理论基础。” <br />
<br />
所谓华北座谈会是1945年2月至7月间，在中共七大召开前后，在延安同时召开的一个会议，历时43天。因此，毛所谓“华北座谈会后”，当然只能是七大之后。如所周知，七大把“毛泽东思想”当成了全党的指导方针。刘少奇在《论党》中强调指出： <br />
<br />
“党章的总纲上确定：以马克思列宁主义的理论与中国革命的实践之统一的思想--毛泽东思想，作为我们党一切工作的指针，反对任何教条主义的与经验主义的偏向。” <br />
<br />
“我们的毛泽东同志，不只是中国有史以来最伟大的革命家和政治家，而且是中国有史以来最伟大的理论家和科学家，他不但敢于率领全党和全体人民进行翻天覆地的战斗，而且具有最高的理论上的修养和最大的理论上的勇气，他在理论上敢于进行大胆的创造，抛弃马克思主义理论中某些已经过时的、不适合于中国具体环境的个别原理和个别结论，而代之以适合于中国历史环境的新原理和新结论，所以他能成功地进行马克思主义中国化这件艰巨的事业。 ” <br />
<br />
在荣任了“中国有史以来最伟大的理论家和科学家”、“具有最高的理论上的修养和最大的理论上的勇气”、“成功地进行马克思主义中国化”之后，才想起来“搞点马克思主义”，无乃太晚乎？这算是什么笑话？ <br />
<br />
有趣的是，“反对经验主义”乃是延安整风的口号之一，与“反对教条主义”相辅相成，周恩来等人就是以此罪名被整肃的，刘少奇在上引语录中也指出了“反对经验主义偏向”是毛泽东思想的一个特点。然而任何一个稍懂哲学的人都知道，所谓“经验主义”根本不是毛说的那个意思，正如毛反对的“自由主义”与真正的自由主义八万竿子打不着一般。 <br />
<br />
更可笑的是，后来毛生吞活剥地看了点哲学书，自己也发现弄错了，在庐山会议上说： <br />
<br />
“如任弼时讲的，说我是狭隘经验主义，我就不承认。你如果承认，但还不知道经验主义是何物，以为做点工作，有点经验就叫经验主义，但在哲学体系上，并不是这样理解的。不懂得经验主义是指马赫唯心主义经验论。《唯物主义与经验批判主义》这本书，就是列宁批判马赫的，不容易读，然而必须读。此书反对波格丹诺夫、卢那察尔斯基，他们悲观失望。1905 年党涣散，列宁自己长期研究多少年，才写出来的。因为理论基础不一致。” <br />
<br />
可怜的是，即使在看了列宁的那本书后，他总算明白“以为做点工作，有点经验就叫经验主义”是错的，却仍然没本事搞明白经验主义的概念，以为马赫主义囊括了经验主义的所有流派，而且它们都是唯心论！ <br />
<br />
好在那时他还比较诚实，坦承自己不懂理论： <br />
<br />
“我也不懂多少理论，不是教授，只是知道一些。教授要读很多书。我书读得少，是些什么意思，大体懂一点。如杜威主义不懂，太复杂，简单意思懂一点。柏格森主义、无政府主义、唯心论、哲学史，看过一些，没有作过深入研究。看书和研究是两回事。冯友兰有研究，才写出《中国哲学史》。看了，不等于研究。因此，马克思主义、辩证唯物主义、历史唯物主义你并不清楚，即基础不懂。根据这个基础的另外一些学问，也就不了解。” <br />
<br />
连杜威主义那么浅显的玩意都看不懂，觉得“太复杂”，这种理解力，能看懂康德、黑格尔的书么？而不懂他们的学说，又怎么可能理解马克思的哲学思想的来龙去脉？怪不得他不喜欢看理论书，因为“太硬”，超出了他贫弱的理解力： <br />
<br />
“我也如此，读书少，后来养成读书兴趣，一拿就是历史、小说、笔记，这些较柔和。理论书太硬。《政治经济学》我就没读过，陈伯达也没有读过。要养成读书习惯，各种书都读，包括文学、历史、法学、心理学、李森科、摩尔根。”<br />
（未完待续）</div>

							
							
							
							
