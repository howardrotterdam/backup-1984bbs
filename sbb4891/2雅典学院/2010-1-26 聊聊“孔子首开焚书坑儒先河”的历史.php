																											<div id="postmessage_452163" class="t_msgfont">孔子焚书毁中华古文明（续）<br />
<br />
&nbsp;&nbsp;据汉《纬书》说孔子将3240篇古籍“约”掉了3120篇，仅剩120篇！使后人对前史望尘莫及，因此成了断代史。另据“[凤舞九天，龙缰行天下]论题：孔夫子烧书烧出功来了吗？孔夫子――“删书断自唐、虞，则唐虞以前，孔子得而烧之。《诗》3000篇，存311篇，则2689篇，孔子亦得而烧之矣。”――“而存者为经，身尊道隆为天下后世法。”――大宗师心存2689个结，2500年以前的中国景观历史成了断代史。图书大抵是被烧掉了！！！”<br />
<br />
根据以上的考证，周朝丢失的图书史料只能受老子和孔子两个人的行为左右。在楚国避难的王子朝要忙于复政及维持军队生存，说他用宝器与周边各国换生活用品是可能的，但说他在暴死前还有心思藏书，则不大可能（但也不能排除他或他下属藏匿手头的重要文献）。战国时“百家争鸣”所依据的是各诸侯国的抄件，不可能是原件，被韩非子称为《周书》的《老子》之各种版本就是各国抄书件的明证。<br />
<br />
因此，对周朝丢失的图书史料踪迹可作如下结论：<br />
<br />
一、孔子烧毁了大部分（没有抄件的就永远消失了），但仍有部分极为重要的图书史料被埋藏在孔子的旧宅范围中（墙壁、地下或周边地区）。<br />
<br />
二、老子在楚国对孔子说：“夫，不忍一世之伤而傲万世之患抑固窭。”说明老子把复兴中国的希望寄托于遥远未来，老子肯定要有远见地藏书在他行程的某地。包括：A、洛阳地下或周边地区；B、任“征藏史”随王子朝去楚的居地；C、由楚返周的沿途地区；D、“南之沛”――中牟县一带；D、去秦国的沿途；E、在秦国的居地。<br />
<br />
三、周时各诸侯国都有自己的国家“图书馆”，都保存着相当部分的周朝史料，被埋藏和流失在民间的史料很多，应该说绝大部分都是真的，不过古代各国文字相异，且都是抄书或刻印，不准确性难免。因此，重新审视被定为“伪书”……十分必要。在各诸侯国所在地也可发掘部分史料。<br />
<br />
作者认为这是一项大工程，它关乎中华民族命运继承的大事大非问题，有志者不可不留此心机。<br />
