													<h2>聊聊“孔子首开焚书坑儒先河”的历史</h2>
						
						
																											<div id="postmessage_452107" class="t_msgfont">一、据西汉“独尊儒术”的《纬书》记载，孔子将古传的3240篇古《尚书》删毁为 120篇，古籍因此丧失了96 /100 以上！造成中国周厉王以前的历史断代，官道理论几乎损失殆尽。孙中山在《建国方略》中说：“周以前为一进步时期，周以后为一退步时期。”就是由“单氏取周”和孔子对前史“约史记而修春秋，赞易道而黜八索。”的“朋党”党封建造成的。<br />
&nbsp; &nbsp; 二、《庄子・人世间》同代狂接舆批评孔子造成周朝“德之衰”！<br />
&nbsp; &nbsp; “孔子适楚，楚狂接舆游其门曰：凤（龙指圣凤指贤）兮凤兮何如德之衰也，来世不可待往世不可追也。天下有道圣人成焉，天下无道圣人生焉。方今之时仅免刑焉：福轻乎羽莫之知载，祸重乎地莫之知避！已乎（完了）已乎临人以德，殆乎（危险）殆乎画地而趋。迷阳（指孔子谄术）迷阳无伤吾行，吾行S曲（指思想裂隙）无伤吾足。山木自寇也，膏火自煎也。桂可食故伐之，漆可用故割之。人皆知有用之用（指唯利是图），而莫知无用之用（指科学技术）也！”<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;五、《庄子・天运》孔子“规”劝老子反“无”：“孔子见老聃，归，三日不谈。弟子问曰：“夫子见老聃，亦将何规哉？”孔子曰：“吾乃今于是乎见龙！龙，合而成章，乘云气而养乎阴阳。予张口而不能啵ㄒ粜・合），予又何规老聃哉？””&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 老子文是公元前 522年周景王改革周朝 300年来政策钦定的“令德”，因为这一年《左传》记载：鲁大夫闵子马公开攻击周朝不学周礼，歪曲老子改革周礼的“无学”理论是“不学”。老子文也是郑国子产“铸刑鼎”后于公元前 522年周景王为天下“铸无射”钟（《国语》“伶州鸠对曰：……无射，所以宣市哲人之《令德》。”）的律文，因为这一年《国语》中记载单穆公反对景王“铸无射”钟，他攻击老子文“有狂悖之言，有眩惑之明，有过慝之度……三年之中而有离民之器二焉，国其危哉”！也因为钟上的律文无先后之分，造成了帛书抄本“德经”在先，而其它抄本“道经”在先的差别。正因为以上的原因，战国时期的韩非子才称老子文是“老子贵虚静而重变法”，说单穆公政变是“亲下以谋上”，说老子文是《周书》，因此《周书》“师旷歌‘无射’曰：国诚宁矣远人来观，修《义经》矣好乐无荒。”《文子》老子曰“河出图（义图），洛出书（义书）。”留传下了春秋末期各国对周《义经》、《令德》、《周书》或《老子》的不同抄本。&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; <br />
&nbsp; &nbsp; 中国两千五百年的“患抑固窭”告诉现代人，当年周景王“铸无射”――“宣布哲人之令德”无可非议，是历史的必然！孔子谄上谄下的谄术，先谄住鲁国的孟僖子、季武子得以“从老聃助葬于巷党”谄住老子，接着又谄住单穆公“毁道德以为仁义”结“朋党”政变而“单氏取周”。“单氏取周”万恶不赦，倾竹难书！<br />
&nbsp; &nbsp; 《韩非子・外储33》：“仲尼曰：与其使民谄下也，宁使民谄上！”。据汉《纬书》记载，孔子在“单氏取周”“修春秋”时将中国古文献3240篇烧掉2689篇，剩下的不足 4/100！“删书断自唐、虞，则唐虞以前孔子得而烧之。《诗》3000篇存 311篇，则2689篇孔子亦得而烧之矣。”造成中国周厉王以前的历史断代！所造成的人财物时流失拖住了中国发展的后腿，给中华民族几千年的精神传统造成了毁灭性的损失！<br />
<br />
单氏取周 ――&nbsp; &nbsp;<br />
&nbsp; &nbsp; 单穆公是周景王时朝一个守旧派，他反对太子晋、周景王、老子“修义经”“铸无射”撰“周书”的官道理论建设，说这“有狂悖之言，有眩惑之明，有过慝之度……三年之中而有离民之器二焉，国其危哉！”。<br />
&nbsp; &nbsp;&nbsp;&nbsp;周景王去世，公元前 520年 6月11日的葬礼上，单穆公发动了政变，他首先杀死了儒子王大夫宾起（《文子》一书作者）立了孔子，又杀死景王八个儿子，并在楚国暗杀了景王立的王子朝，留下一个听话的小儿子丐继了王位，称周敬王。史学称此为“王朝交鲁”，韩非子称此为“单氏取周”：“周单荼（单穆公）、燕子（刘狄）之此九人者，之为其臣也。皆‘朋党’比周以事其君。隐正道而行私曲，上逼君而下乱治，援外以挠内，侵下以谋上。”又说：“单氏取周……人臣之弑君者也。奸臣闻此蹶然举耳，以为是也。”<br />
&nbsp; &nbsp;&nbsp;&nbsp;孔子得势后先在鲁国杀了勇于直言的少正卯，接着在鲁国烧毁了周朝绝大部分传统图书，连太子晋说的“厉始革典”建立共和的“民之宪言”和他夸赞“古之遗爱”的子产刑鼎文都没给留！据《汉・纬书》记载，孔子在“单氏取周”“修春秋”时将中国古文献3240篇烧掉2689篇，剩下的不足 4/100！“删书断自唐、虞，则唐虞以前孔子得而烧之。《诗》3000篇存 311篇，则2689篇孔子亦得而烧之矣。”造成中国周厉王以前的历史断代！给中华民族几千年的官道精神传统造成了毁灭性的损失！这也是老子西归离周去秦的一个重要原因。<br />
&nbsp; &nbsp;&nbsp;&nbsp;“单氏取周”以后，社会风气大变，提倡从有到有的“有学”“有为”让人依赖投机，盗贼横行。各诸侯国视孔子像过街老鼠“逐于鲁，削迹于卫，穷于齐，围于陈蔡，不容身于天下”。<br />
