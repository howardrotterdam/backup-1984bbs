													<h2>回复 25楼 光明的格里高利 的话题</h2>
						
						
																											<div id="postmessage_569673" class="t_msgfont">我想提醒一下喜爱柏杨的读者们，世界上很少有什么东西是非黑即白的。<br />
<br />
柏杨的思想喜欢搞非黑即白的对立――不是&quot;现代&quot;的、“民主”的，就是“酱缸中国的”、“专制余毒的”，举凡“民主的”就是“进步的”，这样的思维方法忽略了中间的渐变地带。<br />
<br />
柏杨鼓吹激进的“社会现代化革命”，这在他对秦商鞅变法的赞扬中就可以看出来。他没有看到“民主”“自由”“平等”“公正”这几个概念间的内在紧张，而是将它们大而化之地统一到“现代性”当中去。<br />
<br />
奢侈的愿望说“柏杨其实还是很传统的，和五四那些人一样，都是将自己从传统得来的民主、自由、公平、法治的半瓶醋心得，用现代语言包装一下，回过头去反对传统”对我有点启发。柏杨和五四一代，乃至于中国近代更早的革新者的共同之处在于他们都有一种“赶超”的紧迫感，因此“追逐现代性”成为了他们共同的价值准绳――凡是现代的，都是好的；凡是传统的，都是现代道路上的阻逆。<br />
<br />
私以为胡适是第一个跳出这个陷阱的重量级文化人物，胡适的独特性在于他有一套根植于西方哲学教育体系的分析方法，使他更能以一个“第三者”的角度来观察中国的现代化进程。当初五四的时候，陈独秀提出的是“全盘打碎”，胡适提出的是“（以西方之方式）整理国故”，你们是要走陈独秀的路还是要走胡适的路？某党当年在陈的路上走了挺远。<br />
