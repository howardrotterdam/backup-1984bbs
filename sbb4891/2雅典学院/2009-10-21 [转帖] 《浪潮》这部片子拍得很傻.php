																											<div id="postmessage_293214" class="t_msgfont">《浪潮》这部片子拍得很傻<br />
nkpoper @ 2009-10-21 0:57 阅读(188) 评论(0) 推荐值(0) 引用通告 分类: 与辩论有关<br />
　　《浪潮》这部片子我没看，只看过简介，大意就是：一个老师带一帮学生搞“极权主义”模拟，几乎搞到弄假成真的故事。以此来警告世人：极权主义离我们有多近。<br />
　　这部片子很傻。人性有各种弱点，只要潜移默化，什么毛病都能诱发出来。只要有人愿意跟你作试验，你也不怕试验对象事后告你，就行。任何一种类似“癔病”或“集体癔病”的症状都可能诱发出来，又何止极权主义？要拍片子警示世人，拍得过来吗？<br />
　　看看各宗教“虔诚的教徒”干出来的那些神经病事，你就知道人性有多软弱了。有的人不吃饭、饿着，有的人不穿衣服、冻着，有的人禁欲，有的人搞男女双修。有的人信教信到“喜主狂热”，大白天看见上帝，幻视幻听；有的人练功练到直接跳河（就他妈的是普通气功），等等等等。但是，我们不能在教堂外边贴个条“信教要谨慎，虔诚会出偏”吧？<br />
<br />
　　历史上，人类选择极权主义，无一例外是因为有某种社会需要，而不是因为欣赏被管束的感觉。是的，“集体主义”给人以力量感，“极权主义”给人以依靠感，这些都是人类的本能；但人类也有与此相反的本能，喜欢自由，不喜欢被管束，好逸恶劳，对同一集体的人斤斤计较，等等等等，也是人类本能。<br />
　　没有哪种本能会单独发挥作用。人们喜欢吃饭，不等于会撑死；人们喜欢性交，不等于会干死；人们喜欢集体主义，也不等于会导致独裁。除非另有原因。<br />
<br />
　　历史上，人类从来不是为了极权主义而选择极权主义，正如人类不是为了“有政府”而建立政府一样。一般而言，或者是因为社会面临紧迫的社会问题或军事压力而选择极权主义，比如大经济危机导致纳粹上台；在成熟的民主国家，这种情况下照样会减少民主，增加管制，只不过此后的走向不同罢了。另一种更常见的情况，则是政策失败的政党拒绝下台而导致极权主义。<br />
　　用《浪潮》这样的片子来暗示极权主义的起源是幼稚的。诱导人们的集体荣誉感并以此来贬低集体荣誉感，就如同老师诱导学生大规模淫乱，搞出毛病来以后，再以此训诫世人“要禁欲”一样，一样无耻。<br />
　　除了无耻以外，《浪潮》这部片子还阻碍了人们利用集体荣誉感来进行正义事业，虽然这在正常的民主国家的正常时期还是无所谓的，因为这种情况下人们不需要花太大的力气来推进什么事业，但这也正是《浪潮》这部片子的欺骗性和巨大的潜在危害性所在。</div>

							
							
							
							
