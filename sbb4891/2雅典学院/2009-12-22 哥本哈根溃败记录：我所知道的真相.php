																											<div id="postmessage_387447" class="t_msgfont">作者：李雁<br />
【绿色和平气候与能源项目，学环境出身、研究生毕业后顺理成章的去环保局工作，两年后怀揣对全球变暖的一腔兴趣投奔绿色和平。现在主要负责气候政策、影响等工作，2007年到2008年作为绿色和平国际气候谈判代表团的成员参加巴厘岛、波兹南等各次谈判会议】<br />
<br />
<a href="http://icare.greenpeace.org.cn/viewnews-197" target="_blank">http://icare.greenpeace.org.cn/viewnews-197</a><br />
<br />
<br />
哥本哈根气候大会延时到周六下午正式敲槌结束。诚实的讲，从周四事情开始显露失控的迹象，到大会最后成为一次名留史册的彻底溃败，没有人能完全搞清楚到底哪里出了错。194个国家怎么就错过了千载难逢、一生一次的机会？每个身处漩涡中的人都忍受着震惊、挫败和困惑。<br />
<br />
周六晚上我原本写好了一篇博客，试图逃避郁闷之情，开始理性反思。但今天上网看了一下国内的新闻，发现居然没有人讲的清楚最后两天发生了什么！多数报道非常含糊，或停留在周五下午的状态，甚至还有人以为哥本哈根会议成功了！？<br />
<br />
不了解真相，如何开始反思？如何能避免所有人一次再次的消耗宝贵的时间、延误战机？在最后两天NGO被大量阻拦入场的信息失衡局面下，幸运的拿到了入场券的我、爱伦和小军，现在有责任站在一个相对独立和清醒的立场上向一直以来关注着哥本哈根的你们还原这个真相！虽然并没有亲身经历每一个瞬间，但我相信这将是我们三人在场内眼见耳闻汇集的一份重要史料。<br />
<br />
2009年12月17日，周四，<br />
<br />
我觉得事情是从周四开始发生戏剧性转折。在那之前的部长级会议没能达成任何有意义的共识，两份主席案文上面打满了代表异议的方括号，动弹不得。而在美国的宣传和施压之下，没有人再去关注这个对全球变暖负有最大责任国家的减排目标问题，从这时开始，中国的减排信息透明问题竟然成为会议的焦点！第一周游刃有余的中国代表团开始陷入合围的被动处境。<br />
<br />
周四，首脑峰会开始，重要国家的首脑许多提前一天抵达进入对话。人们寄希望于首脑们解决分歧最严重的问题。现在看来，这可能是一次危险的赌博。<br />
<br />
AM：<br />
<br />
周四，提前达到的美国国务卿希拉里发表讲话宣布美国支持发达国家整体作出长期的减排资金承诺，到2020年达到每年1000亿美元并持续下去，美国将承担相当部分。在幕后沉默至今的美国这一表态，受到广泛但谨慎的欢迎（包括中国在内）――没人知道这会不会是张空头支票，以及美国下一步棋会怎么走。<br />
<br />
这一天上午，有媒体报道说一位匿名的谈判代表透露中国人称会议只能达成一个“很短的政治宣言”，引起一场公关危机。媒体中心里传言这是美国人放出来的风。虽然中国发言人很快澄清，但事态很明显――最后的blame game大戏登场。于此同时，关于中国信息透明度的讨论进入白热化。据说中美两国在一周的公开对抗后，这一晚终于私底下找到了双方都可以接受的解决方案。<br />
<br />
周四的另一个新闻，是联合国为首脑们准备的一份背景分析文件被媒体泄漏，显示出按照现在所有国家公开提过的减排承诺，包括中国印度这样的相对排放控制目标在内，全球升温将达3度。<br />
<br />
PM：<br />
<br />
周四晚是丹麦女王主持的首脑晚宴。传闻宴会过后法国和巴西会拿出一份三页纸的草案，邀请包括中美、欧洲等大国在内、也包括一些小岛屿国家和非洲国家在内的28位国家元首讨论。一直坚决对抗不够透明和平衡的丹麦案文的中国，对如此重要的会议只派了外交部副部长参会。有人开始猜测中美两国首脑在“较劲”。<br />
<br />
 <br />
<br />
2009年12月18日，周五<br />
<br />
AM<br />
<br />
周五一早的重头戏是温总理和奥巴马总统的大会发言。温总理的措辞出乎意料得强硬――不管这次会议成不成，我们自己定的目标一定会实现，不以任何其他国家为转移！后来外交部散发的正式讲稿上，并没有这句话。而且讲话时温总理看得出非常生气……之后奥巴马的发言则火药味更浓，他干脆把某些国家的数据透明度与世界减排目标和气候资金并列来谈，暗示中国不接受信息公开是大会的主要阻碍因素。他的发言也跟公开讲稿有很多不一样。听完这剑拔弩张的遥相问答，我和爱伦面面相觑，只说得出一句话：事情不妙了。<br />
<br />
对奥巴马就差指名道姓的攻击，据说中国代表团大为光火。在前一天就已经差不多谈妥了的情况下，这样的公开打击近乎“背信弃义”。<br />
<br />
前一晚的“哥本哈根临时协议Copenhagen Accord”草案，周五早上在所有信息灵通的媒体和NGO之间流传。果真是政治宣言，内容很糟糕，没有发达国家的整体减排数字――只说2010年1月份各国应该提交各自的承诺，唯一值得安慰的是给出了哥本哈根之后一年的谈判时间表，并明确说2010年底要达成的是两份具有法律约束力的议定书。<br />
<br />
看到这份草稿，所有人开始明白，无论如何一份完整的协议是无法达成了。此刻起一切将开始围绕这份政治宣言的质量旋转。<br />
<br />
在全程对首脑发言现场进行直播的大屏幕上，我们看到中、印、南非和巴西的首脑一起出去密谈。听说温和奥巴马发言之后也谈了一次，白宫声明说“长达53分钟”，并“很有进展”。<br />
<br />
PM：<br />
<br />
下午三四点，惊天消息传来：奥巴马闯进了中印巴西南非的基本四国会议，跟四位新兴经济国家的领导人直接斡旋。听说他说了两遍“我能进来么？”，并直接坐下。然后听说巴西总统卢拉离开。四个小时后，奥巴马在CNN直播中宣布他与主要发展中国家达成了共识。一份全新的“临时协议”出炉。随后空军一号起飞，延长访问5个小时的温总理也终于赶赴机场回国。<br />
<br />
晚上10点左右，我们也终于拿到了这份五国版“临时协议”。令人遗憾的是，这版比早上的28国草案更糟糕――2050年长期减排目标不再提，就连明年底要达成协议的语言也消失了。但可以看出那个闭门会议的成果：信息公开数据透明那一段写的很明白。中美都达成了共识，在很多人看来就算成了。媒体纷纷发出“哥本哈根终于形成共识”的新闻稿，各个机构迅速作出回应。相信现在很多对哥本哈根错误的乐观报道，就是从那时的新华社通稿而来。<br />
<br />
然而，这一切还没有结束。实际上，从奥巴马宣布协议达成这一刻起，败局已定。<br />
<br />
这样的协议，没有征求欧盟意见，绝大多数的发展中国家也毫不知情。欧盟和发展中国家集团分别召开紧急会议讨论是否接受。但中美和其他发展中大国元首已经离场，没有可能再对协议内容展开讨论，这是不争的事实。<br />
<br />
午夜前，发展中国家集团的主席――苏丹大使来到媒体中心，召集媒体宣布大部分发展中国家将不会接受这份强加的“协议”，里面既丝毫不能反映全球减排的雄心，也没有对最脆弱国家的关注。宁愿明年重头再谈。欧盟宣布他们将维持现有的20%减排目标。至此，德、法、英推高欧盟整体目标到30%的努力宣告失败。巴罗佐在发布会上苦涩自嘲：都没有人来跟我们提过这个要求。<br />
<br />
午夜12点，公约缔约方大会闭幕式正式召开。这会议不再讨论任何具体内容，而围绕着两点进行：这份几个国家元首签署的跟联合国正式程序毫不相干的政治协议，大家要拿它怎么办？明年还怎么谈？<br />
<br />
2009年12月19日，周六<br />
<br />
除了坚持为自己的国家振臂高呼的马尔代夫总统以外，所有元首都飞走了。政治协议之外的100多个国家愤怒、疑惑、挫败。对于气候谈判未来命运如何的讨论持续了一整夜。我早晨7点半终于坚持不住离席打盹，一路上看到面如土色、脚步滞重的代表们面无表情的缓缓走过，恰如一个外国记者在博客上形容的：这是个活死人之夜。<br />
<br />
谁都不敢想象最后的时刻。<br />
<br />
欧盟看起来已经接受了现实，打算支持五国临时协议。英美强烈要求把它作为大会的一个决议保留下来。对广大发展中国家，最后这一点可怜的政治共识，是要彻底丢进垃圾桶不留一点痕迹，还是至少有一点算一点，成为艰难的选择题。拉美国家排队发言，坚决反对把它作为大会的决议。苏丹坚决要求不在大会决议里提一个字。马尔代夫则夹在中间，恳求通过某种形式保留住这个共识，里面至少有可以立即执行的300亿美元。<br />
<br />
早上12点回到会场，正赶上大会将要得出结论。在最后的决议文件里，写到“注意到哥本哈根临时协议”，并把它放在附件里供参考。<br />
<br />
下午3点左右京都议定书缔约方的会议也在一片死气沉沉中结束了。可惜的是，这么长久的艰辛和期望，到了最后，只落在两页薄薄的决议上：大意是，明年接着谈，到墨西哥的16次大会汇报结果。但会有什么结果、是否是法律约束力的议定书，只字未提。<br />
<br />
哥本哈根气候大会在筋疲力尽的旋转三天之后，终于到底。<br />
<br />
耳闻大会结束的锤声，面对全球亟需的气候保护协议的流产，没有人哭泣，出奇的安静。全球变暖不会因此而停止，气候谈判也并不以为为终结点。巨大的失败带来的是巨大的震撼，来不及调动个人感情。<br />
<br />
我一直在想，未来人们会如何落笔书写这两天。昨晚，走在积雪的哥本哈根街头，我开始相信历史会铭记这一天。194个国家、120位国家元首、2万5千参会代表、数十万、乃至上百万走上哥本哈根和全球街头的志愿者，为了一个共同的目的认真奋斗过。我们有了几乎能想象到的最好的条件，最终却仍被分歧隔绝在世界的彼端。一定有什么深层次的思考和决定尚未发生。这样的两周，在每一个国家都会留下痕迹，带来一些改变。只是此时此地，身处历史中的我们尚不能明白。<br />
<br />
下一步是冷静反思，坚定决心。</div>

							
							
							
							
