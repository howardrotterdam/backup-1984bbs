													<h2>环球时报：朝鲜飞机事件不代表中国空防能力低</h2>
						
						
																											<div id="postmessage_789909" class="t_msgfont">17日下午，一架朝鲜小型飞机在辽宁抚顺坠落。而据有关部门19日消息称，朝鲜已对此事件进行说明，称失事的朝鲜军用飞机系机械故障、迷失航向而坠毁，并就此对中方表示歉意。但民间仍然有许多疑问和猜测。有人猜这是被中国击落的，也有人质疑中国的空防水平，对一架别国飞机长驱直入不太理解。针对这些问题，笔者认为小型飞机进入中国并坠毁并非表示我国空防系统薄弱，目前中朝双方公布的消息从技术层面看是可信的。 <br />
 <br />
<br />
　　疑问一：飞机是否是被击落的？看上去完全不像。因为事故现场的飞机主体是完好的，没有任何受到炮弹或导弹打击的迹象，从而可以认定飞机不是“摔”下来的。从现场景象判定，飞机是强行降落的。飞机落地后并未发生爆炸，这表明飞机降落时燃油已经耗尽。从这点可以看出，飞机是在燃油耗尽后，飞行员主动强行降落的飞机。<br />
<br />
　　疑问二：飞机闯入中国领土是否表示中国空防能力低下？显然不能做出这样的结论。一是这架飞机没有任何迹象表明其在执行军事任务。从全球有关国家使用飞机侦察别国情报的历史看，任何一个国家都会使用技术最先进的飞机来实施对某特定国家进行侦察。从“朝鲜飞机”事件中，这架飞机的外部特征看，并不是一架投术先进的飞机。在任何一个技术先进的雷达屏幕上都会显示出其低下的技术性能。就这点而言，我们可以判定该飞机对我进行情报侦察的可能性不大，也不可能单独执行其它军事任务。以笔者的经验，在这种情况下，中国防空系统并没有启动作战命令的急迫需求。事实上，这也表明中国不希望在“天安”号事件后的敏感时刻再出现“紧张事件”的克制之态。<br />
<br />
　　事实上，当飞机出事后，人们很容易就联想起1987年一架德国小飞机停落到苏联红场的“鲁斯特事件”，但这并不意味着苏联空防能力低下。在该事件出现四年前，苏军曾经在其远东的萨哈林岛上空击落了一架韩国民航波音747飞机，造成机上269名乘客死亡。之后，苏军下达秘密指令，在无法判明飞机有军事目的的情况下，禁止向一切民航飞机和体育运动飞机开火。实际上，当年联邦德国青年鲁斯特驾驶单引擎运动飞机一进入苏联领空时，苏防空力量就发现了这架飞机。后来只因发生多种“巧合”，鲁斯特才有机会驾机降落到了红场。<br />
<br />
　　对“朝鲜飞机”事件，笔者认为不应过于“大惊小怪”，避免给某些不友好势力钻空子的可乘之机，也避免东北亚地区的军事形势更加恶化。当然，为了消除人们的 “大惊小怪”，随着对“朝鲜飞机” 事件调查的深入，如果能在适当时机公布更多有关细节也将是有益的。“有关细节”’是指那种不涉及军事安全的那些细节。只要人们了解了“朝鲜飞机”必要的有关细节，人们用这些必要的“有关细节”对该事件进行合理的解析， “朝鲜飞机”可能就会淡入历史中去了。(作者：韩旭东 国防大学教授)</div>

							
							
							
							
