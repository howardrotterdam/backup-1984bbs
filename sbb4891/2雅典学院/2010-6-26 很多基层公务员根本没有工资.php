													<h2>很多基层公务员根本没有工资</h2>
						
						
																											<div id="postmessage_705196" class="t_msgfont">（本人不是标题党，也不是说反话，请全部看完再喷）<br />
&nbsp; &nbsp; 当前社会断裂征兆日益明显，即使是在84这样激进主义不占优势的论坛，不少关于公务员工资的帖子仍然引起了激烈的争论，这些帖子的内容基本上是一些基层公务员晒自己的工资和福利，表明自己生活不过是小康水平，远不是大家想象中的那么奢华。这引起了大家的围观，尽管帖子中提供的数据应该是真实的，尽管有少量的人也表示不应当对公务员整体进行仇视，当前的社会弊端来源于制度缺陷而不是公务员本人的道德水准，但是压倒性的声音仍然是对公务员的辱骂。<br />
近年来的公务员热是不争的事实，但是公务员职位的吸引力更多来源于稳定，而不是高薪。公务员待遇的差别是非常大的，发达地区和欠发达地区不一样，清水衙门和“肥缺”不一样，领导和基层办事员不一样；其差距之大不亚于欧洲与非洲之间的差距。一些地方公务员年薪6万还觉得收入低（与本地其他职业相比），一些地方公务员年薪不足2万依然觉得心满意足。总起来说领导干部（基层大约以科级为标志，中央和省级职务应该更高）的待遇是不错的，而基层公务员的收入则参差不齐，个人感觉有相当部分与熟练工人、普通公司白领接近。这也和前面的帖子的结论相符。<br />
&nbsp; &nbsp; 一、关于贪污问题。<br />
&nbsp; &nbsp; 尽管现在的贪污腐败触目惊心，但是从绝对数量上讲，贪污受贿的还是少数，对于大多数无权的基层人员来说，不存在贪污受贿的收入，一刀切地质问“你们的黑色收入有多少”并不合理。这不是为腐败辩护，现在的腐败问题已经严重到不可忍受的程度了。<br />
&nbsp; &nbsp;&nbsp;&nbsp;举个通俗的例子：中国矿难（腐败）十分频繁，但是，真正死于矿难的工人（腐败分子）在全部工人中还是很小的比例，所以说尽管我们主张大力加强煤矿的安全生产（反腐败），但是，到煤矿上看一下，今年有1000名工人，明年其中99%还活着也是正常的现象（基层公务员没有贪污），这不是有人在造假，因为一年死10名工人（当前的腐败水平）已经是不可接受的了。<br />
&nbsp; &nbsp;&nbsp;&nbsp;二、比贪污更可怕的是逼人作恶的制度<br />
&nbsp; &nbsp; 很多人都骂公务员，怎么跟他们解释都听不进去，似乎只要是吃上皇粮马上就成了十恶不赦的恶棍。但是，如果把这些骂公务员的人――如在世关羽、蒙面佐罗――放到公务员的岗位上去会怎么样，拜托别说你不屑去干，就假设拿枪逼你去干好了。我能断言，这些人在踏上公务员的岗位上之后将会比现在的公务员更加穷凶极恶，因为这是当前制度的必然结果，即使把所有的公务员全部干掉，全换上雷锋、焦裕禄（呵呵，俺知道那是宣传，姑且假设确实存在那种道德毫无瑕疵的完人把）一样不能解决问题。这些人为什么变了呢？很重要的一个原因就是当前财政预算的管理方式。请看：<br />
&nbsp; &nbsp;&nbsp; &nbsp;三、为什么说很多基层公务员根本没有工资<br />
&nbsp; &nbsp;&nbsp;&nbsp;人称公务员是吃“皇粮”的，很多人因此（包括以前的我）想当然的认为，公务员的工资和政府部门的经费来源于财政――或者说是来源于税收。而实际情况是，并非完全如此，有相当的部门，――如很多是执法部门――相当部分的经费财政并不管。比较极端的一个例子，某执法部门每年财政的拨款大约是人均2400元左右，你没看错，是每人每年，不是每个月2400元，这2400元不仅包括人员的工资、福利，单位所有的办公经费甚至基建开支都要从里面出。<br />
&nbsp; &nbsp; 因此对公务员满心愤怒的你走上公务员的岗位的时候，你首先面对的问题就是经费从哪里来？上级拨付的全部经费还达不到当地的最低工资水平，也许你很高尚，家庭也算富裕，你可以不要任何报酬免费为人民服务，但是单位的水、电、通讯、交通、保安哪一样不要钱？写个报告也要买纸打印吧。当然，上级既然如此安排，肯定是有原因的，经费的缺口也是有来路的。第一种办法是找当地政府化缘，这是个垂管部门，找当地政府化缘的直接后果就是在执法的时候必须要瞪大眼睛，当地政府的关系企业是绝对不能去管的，毕竟吃人嘴软。第二种方式是罚款。尽管多年以前经费管理就实现了“收支两条线”，也就是说罚款不是自己收，而是直接通过银行上交财政，但是实际情况是交上去之后财政会返还给你们的，当然返还的钱不能全部拿到，上级部门会按比例层层留成的，但是毕竟是来钱的门路。这种情况非常普遍，从这种意义上讲，很多公务员根本没工资，不仅自己的工资，甚至是单位的经费都要自己想办法。为了这种目的去罚款就必然导致下达创收指标――如果达不到某个数额单位的经费就没有保障，当然你也没有福利甚至是工资。――单位下达的创收指标有时要高于本科室的工资、福利和经费的总和，一切都要看单位总体的经费情况才能确定。例如每年10万元的任务，约定可以提取2万元经费，那么每上交一笔罚款，科室都能得到20%的经费，只要能提供合法的发票，单位不会管这钱到底是怎么花的。但反过来，如果你没弄到钱，对不起，所有的办公经费（包括雇佣临时工的工资）都要由你自己承担，单位不会掏一分钱的，甚至还会被削减福利，扣工资。<br />
&nbsp; &nbsp;&nbsp;&nbsp;也许你说，nnd，罚老百姓的钱给自己发工资，这事我不干。呵呵，政府是干什么的，为社会提供公共服务使其最重要的职能。你不去罚款，自己不拿工资是小事，因为没有经费导致本部门职能的瘫痪才是大事！就好比警察，不给你经费你就撂挑子，歹徒上街杀人放火你也不去管就很高尚吗？即使作为基础警察你可以撂挑子，但是作为警察局长撂挑子的话真的就对吗？在宪政国家，可以通过合法的程序来保证警察的经费，但是在坛，罚款是唯一的途径。从这种意义上讲，作为一个有责任感的公务员必须去罚款来筹集经费，但这个口子一开，“三乱”甚至钓鱼执法产生的就是必然的了。<br />
&nbsp; &nbsp;&nbsp; &nbsp;四、公务员在抱怨什么？<br />
&nbsp; &nbsp;&nbsp; &nbsp;对于公务员晒收入的帖子，很多人都在质问：“既然收入不高，你为什么不辞职？”其实这个问题完全可以用来问血汗工厂的工人：“收入这么低，你为什么不辞职？”在就业形势如此严峻的今天，公务员的吸引力很大程度上来自于稳定感，基层公务员收入一般，却是很多人都在抢的职业，血汗工厂的工人收入微薄，但毕竟有口饭吃。这样问的人其实误解了公务员发帖的动机。他们发帖主要不是嫌钱少――嫌钱少该去找领导反映，上网有用么？――而是对不分青红皂白仇视基层公务员思潮的一种反应，表明基层公务员不愿称为不合理制度的替罪羊。<br />
&nbsp; &nbsp;&nbsp;&nbsp;在现有体制下，基层公务员确实是一份比较稳定，收入不错的工作，不少老百姓也很希望能得到这份工作，但很多政府部门胡作非为，其根源不在于其个人的道德水准，而是来制度使然，当今社会矛盾日益激化，公职人员形象江河日下，以致发生百姓暴力伤害公职人员的行为社会上居然一片叫好声，当今仇官（存在就是合理的，很多当官的确实可恨）意识已经很大程度上被简化成仇公务员。但实际上基层公务员离既得利益集团还差得很远，绝大多数人都是不反对，甚至是支持改革的。例如前几年湖北桃园的工商人员就走上焦点访谈揭露本系统的乱收费（因为自己的利益也收到了损害），08年取消个体户管理费，百姓固然支持，可大家不知道的是连基层的工商人员居然也对此欢呼雀跃。可见想创造一个更好的社会，应该团结大多数的基层公务员一起努力，而不是再搞一次革命，把现有的公务员全部打翻在地，再把新一批人推到同样的位置上去！<br />
