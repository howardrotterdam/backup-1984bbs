质疑之处：<br />
<br />
一．第二段前面说到“突然，飞机遇到一股强烈的寒流，机翼和螺旋桨上都结了冰，而且越结越厚。不大一会儿，机身也蒙上了厚厚的冰甲。”但是这一段的结尾又说“机长命令机械师打开舱门，把行李一件一件往下扔，好减轻飞机的重量”。既然机身都蒙上了“厚厚的冰甲”，那么机械师又怎么能打开舱门呢？舱门不是被“厚厚的冰甲”冻住了吗？机身上有厚厚的冰甲，那舱门又怎么打得开呢？<br />
<br />
二．如果舱门真的被打开了，会有什么样的后果呢？在空中，机舱内的气压比机舱外的高。如果打开舱门，由于气压的原因，高气压就会向低气压流动，就像水往低处流一样。那么，机舱内没有固定的物体都会飞出舱外去，包括人，这是很危险的。另外，因为是在寒流中打开的舱们，冷空气会迅速进到机舱内，人就会在短时间内被冻僵，很难活动，时间长了甚至会被冻死，在这种情况下，周恩来如何能安全地走到小扬眉面前并自己解下身上的伞包呢？<br />
<br />
三．跳伞是一项专业技术性非常强的工作，动作要领和技术规则不要说一个小孩，就是一个大人在短时间内也绝对不可能学会。这一点经验常识，周恩来不可能不知道，就算是他，没有经过长期正确规范的训练他也不会用降落伞。所以，即使周恩来把降落伞伞包给了小扬眉她也不会用。那么他为什么还要把伞包给了小扬眉呢？如果是要安慰小扬眉的话，他可以把孩子抱在怀里然后告诉她准备和叔叔一起跳不是更能安慰小扬眉吗？他这个成年人使用降落伞不是比一个小孩子更可靠些吗？<br />
