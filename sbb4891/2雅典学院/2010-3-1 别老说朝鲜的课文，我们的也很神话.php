飞机遇险的时候<br />
<br />
<br />
①1946年1月， 周恩来 同志从延安乘飞机去重庆。同行的除了工作人员，还有 叶挺 的女儿小扬眉，她才11岁。<br />
<br />
②飞机飞得又快又稳。透过云层，可以看到积雪的山峰层层叠叠，好像波涛汹涌的大海。突然，飞机遇到一股强烈的寒流，机翼和螺旋桨上都结了冰，而且越结越厚。不大一会儿，机身也蒙上了厚厚的冰甲。飞机像冻僵了似的，沉甸甸地往下坠，还失去了平衡。机翼掠过一座座山峰，眼看就要撞着山尖了，情况十分 严重 。机长命令机械师打开舱门，把行李一件一件往下扔，好减轻飞机的重量，还要大家背上降落伞包，做好跳伞的准备。<br />
<br />
③大家正忙着，忽然听到小女孩的哭声。 周恩来 同志立刻站起来，从摇摆不定的机舱一头，几步跨到小女孩面前。原来小扬眉的座位上没有降落伞包，她急得哭了。 周恩来 同志马上解下自己的伞包给小扬眉背上，还亲切地鼓励她说：“孩子，不要哭，要像你爸爸那样勇敢、坚强，同困难和危险做斗争！”<br />
<br />
④大家看到周恩来同志不顾自己的安危，把伞包让给了小扬眉，都要把自己的伞包让给周恩来同志。周恩来同志用命令的口吻说：“不要管我！大家要沉着，不要慌张……”<br />
<br />
⑤就在这时候，飞机冲出了寒流的包围。在阳光的照射下，冰甲开始融化了，整块整块的冰哗哗地往下掉。飞机渐渐升高了，继续快速平稳地向前飞行。“咱们脱险啦！”机舱里一片欢腾。<br />
