我还是不死心，决定从“威努斯基”这一名字入手查找，结果查到威努斯基市的名实际意思是“野洋葱”，其它再无进展。于是，我决定查“威努斯基”的英文名。<br />
“威努斯基”的英文名是什么？按“野洋葱”的基本意思“wild onion”查找，一无所获。我先查找佛蒙特州的官方网站（<a href="http://www.vermont.gov" target="_blank">www.vermont.gov</a>），终于在“娱乐与旅游”栏目下找到了一个下拉菜单，里面有佛蒙特州的几十个城市名，经过比较，我最终确定“威努斯基”的英文名是Winooski。<br />
我通过“Winooski”查到了Winooski的市府网站（<a href="http://www.onioncity.com" target="_blank">www.onioncity.com</a>）。进入后，我看到网址，看到城市“野洋葱”市徽，看到如“wild onion（野洋葱）”之类的英文词语，我确定没错，Winooski就是威努斯基市。但是我在Winooski的市府网站找了几遍后，还是失望。整个网站连有点历史的小餐馆和小磨房都介绍到了，就是没有介绍到“巨伞”。莫非我那蹩脚的英语看不懂？那“巨伞”的图片总会有一两张吧？可是就连“巨伞”的图片都没有，甚至在别的图片里作为背景“巨伞”都没有看到。这是为什么？<br />
这时，我想到小学课本曾有过的一篇很感人文章（题目我记不清了），叙述苏联宇航员在坠毁前几个小时内的镇定面对死亡的课文，后来被指出是虚假的。有一篇关于科学家通过油画的画面而追源，最后发现巨大汞矿的文章（题目我也记不清了）曾广为传播，后来证明也是虚假的。《中国青年报》曾在某年4月1日登载一个关于允许高学历者多养孩子的愚人节玩笑，被许多报纸当作真实消息广而传之。类似上述的例子举不胜举。<br />
那么《巨伞下的城市》这篇课文会不会也是虚假的呢？是威努斯基市曾有一个这样的设想，最后没有实施？还是这也是一个人们的科学幻想？或者干脆就是一个类似愚人节的玩笑？如今被权威报纸一登，人们也就信以为真了，成了真实的事实，就堂而皇之地成为了我们的教材？<br />
虽然本文可能是虚假的，作为教材是不应该的，但是并不影响同学们从语文角度来学习本文，我们不可因噎废食，就不学本文了。我们还是应该把它作为语文，一如既往地认真学习。<br />
我没有到过美国，我也没有亲朋好友在美国，我只是“两点一线”地生活着，我只是通过网络和自己的头脑来判断，也许是我错了，但愿是我错了。<br />
我希望比我有见识，能到国外走走的诸位能代我去看看，然后告诉我《巨伞下的城市》这篇课文是真的吗？（作者：吴强）<br />
