草帽计<br />
<br />
1934年，中国工农红军开始了举世闻名的二万五千里长征。贺龙同志带领一支红军队伍，由湘西向贵州进发，蒋介石的白军一面死死盯住不放，一面派飞机在天上跟踪轰炸、扫射。<br />
