　　1891年出生于湖南株洲，在他还没有出生时，父母就不幸去世 。他幼时家境贫寒，无力读书，但热爱大自然 ，热爱祖国，富有正义感。他活泼开朗，六岁便会 作诗，十岁就已贯通《齐民要术》。他思想先进，热爱大自然 ，儿时受《劝世良言》一书影响巨大，忧国忧民的他深感社会暗黑、清廷腐败，从此立志报国。《辛丑 条约》签订后，国土沦丧，百姓受难，他积极背书。1908年在家乡建明村组织反清协会，任会长，网罗大批爱国人士，宣传民主共和思想。1910年4月13 日，米价上涨，四处发生抢米事件，他积极发动全村群众进行抢米活动 ，有力地支援了广州的起义。 <br />
