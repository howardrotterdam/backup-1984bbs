													<h2>谈1984BBS关站事件</h2>
						
						
																											<div id="postmessage_770833" class="t_msgfont">欢迎订阅我的blog：<a href="http://hexiecollege.blogspot.com/" target="_blank">http://hexiecollege.blogspot.com/</a><br />
原帖：<a href="http://hexiecollege.blogspot.com/2010/08/1984bbshexiecollege.html" target="_blank">http://hexiecollege.blogspot.com/2010/08/1984bbshexiecollege.html</a><br />
作者：Hexiecollege<br />
<br />
日前，在中共技术部门持续的DDoS攻击之下，知名论坛1984BBS被迫暂时的关站了。1984bbs的站长张书记在他的Twitter上对于此事进行了谴责。1984BBS论坛是中文互联网上的最有人气，最专业的政治论坛之一，虽然它的建站时间不算早（2008年8月创建），却策划出了几种非常有创意的站内项目――大讲堂，博客，新闻社。它凭着出色的管理，出色的创意，在为数不少的中文政治BBS中脱颖而出。论坛分为自由新闻社，雅典学院，开放社会资料室，国家局域网研究所，罗马假日公寓，Live!大讲堂等几个板块。显而易见，站方设计的这几个板块都是出于反对政府审查制度的目的（不仅仅是网络审查，还包括政府对线下社会的审查），新闻板块可以让网友获取被政府宣传部门过滤掉的信息，技术版块可以帮助网民学会穿越GFW，资料板块汇集了网友上传的被禁书籍，电影等资料。<br />
<br />
中共技术部门此次针对1984BBS的DDoS攻击，实质上是其一贯的互联网围剿政策的继续。在此次事件发生之前，它先后整肃了豆瓣网、牛博网、天益网，推行了域名及服务器实名制度，它还动用黑客力量试图进入Google服务器窃取异议人士的资料。在这些中共制造的事件当中，中共的宣传喉舌和技术部门有着惊人的默契配合，他们乐此不疲的，向互联网发动一轮又一轮的“进攻”，他们很注重“战争”的布局和控制节奏。<br />
<br />
互联网围剿,虽然是一场无硝烟的“战争”，却使中文互联网笼罩了一片红色恐怖的气氛，而这气氛让我们为之窒息。<br />
<br />
网上流传这这么一首改编的诗歌：<br />
“他们先是要求域名备案，我没有说话，因为我的域名已经全部备案。<br />
他们接着要求网站内容不能有违禁词，我没有说话，因为我的网站暂时没有违禁词。<br />
他们又要求论坛要专项备案，我没有说话，因为我没做论坛。<br />
他们再要求所有网站都不能有交互内容，我没有说话，因为我的站点是WEB1.0，不能交互。<br />
他们最后拔了我的服务器网线，这时已经没有人替我说话了。”<br />
这首诗正是我们的现状的写照。<br />
<br />
我呼吁，无论是网站站长，还是普通的网民，都不要在中共的进攻面前退缩。我们是多数，而他们是少数。我们是这个时代的英雄，而他们只是一群卑鄙的战犯。我们应该坚持抵抗，为着，保卫我们在网上自由表达意见的权利，保卫我们在互联网上生存的权利。我们最终会赢得光荣的胜利！<br />
