													<h2>中共十八大卡位：薄熙来要取代习近平，江泽民久久不能离去</h2>
						
						
																											<div id="postmessage_472614" class="t_msgfont">中共中央政治局委员兼重庆市委书记薄熙来，是中国内地近几个月来最具人气的政治人物，在海内外媒体上的曝光率甚至超过总书记兼国家主席胡锦涛和中共储君、副主席习近平。薄熙来在他主政下的重庆接连“唱红打黑”的动作，展现了他的智慧和魄力，为他赢得了中共党内外不少掌声支持，却也暴露了他强烈的企图心，引起了党内各方竞争对手的关注，令不少人坐立不安，“犯了嘀咕”，亦因此为中共高层派系博弈提供了最新素材，提前打响了中共十八大人事布局的前哨战。<br />
最高目标：取代习近平当备位储君<br />
<br />
北京政坛消息人士透露，今年六十岁的薄熙来，是在经过认真的算计之后放手一搏，目标是以重庆打黑的成绩为敲门砖，瞄准三年之后现任政治局常委兼中央政法委书记周永康的位置，或现任政治局常委兼中纪委书记贺国强之后的空缺，甚至成为取代习近平的备位储君，“这是薄熙来的最高目标和政治理想，也是薄熙来‘唱红打黑’背后的真正目的”。<br />
<br />
薄熙来以“唱红打黑”的组合拳，不但凸显了自己根正苗红、政治正确的正统地位，而且要以此展现了自己的能力、魄力和谋略，要因此赢得民心，并以民意的支持为后盾，达到自己的政治目标。这位北京政坛人士表示，三年之后的中共十八大，如果还是目前的九常委格局，那么“前面的几个坑包括总书记兼国家主席、人大委员长、总理和常务副总理、政协主席以及主管意识形态的政治局常委等，都已经有人填满，薄熙来在经过认真的分析之后，发现只有后面的中纪委书记和中央政法委书记，他或许还有希望”。<br />
<br />
因此他这是最后一搏，背水一战，不但有可能达到这个目标，而且说不定还有机会夺取大位，在竞争中把习近平比下去，前提是三年之后的中共十八大有政治改革的大动作，学习越南共产党第十次代表大会的经验，总书记也采取差额选举的办法，以民意和党意为依归的理由，摆平利益，竞争上岗。北京政坛分析人士认为，三年之后的中共十八大，假如团派人马和太子党利益无法摆平，胡锦涛并非没有可能作出这样的动作。<br />
<br />
为未来总书记竞争上岗热身准备<br />
<br />
这样的假设，从不久前刚刚召开的中共十七届四中全会发布的公报，或许可以发现一些端倪。公报用数段的篇幅，谈了中共党内民主发展的问题，提出“党内民主是党的生命”，要“完善党代表大会制度和党内选举制度”，但更重要的是首次在中共党的重要文件中提出了“民主、公开、竞争、择优”八个字的选人用人原则，似乎是在为未来中共十八大可能出现的权力争夺预留伏笔，并奠定重要的理论依据和法理基础。<br />
<br />
而这恰恰是薄熙来最后的希望，他或许可以借此冲破原先设下的格局，突破框框，竞争上岗， 实现逆转胜，从而实现他的政治理想。虽然年龄不占优势，但他不怕竞争，也善于竞争，这是他的强项。从中国社会科学院研究生院国际新闻专业硕士研究生毕业的薄熙来，是中共高层少数了解媒体运作的专家，也很善于利用媒体造势，并在中国媒体界有不少老同学、老朋友和众多粉丝。重庆“唱红打黑”开始之后，薄熙来即刻成了中国媒体的新闻人物，毕竟打击黑社会符合老百姓的期待，符合社会的利益，他因此成了不少中国人心目中的英雄。<br />
<br />
这是未来薄熙来竞争中共总书记或竞争政治局常委重要的社会基础。重庆“唱红打黑”行动之后，不少人甚至在网上公开呼吁“薄希来（原文）书记当国家主席或当共和国的总书记，那是十三亿中国人民的大幸运！！建议全国的老百姓祈祷，薄希来书记当国家主席或当共和国的总书记。因为他是国家的希望，人民的希望”。“是啊！兄弟我也是这样想的,十七大薄书记没进常委,感觉好失望!每次从新闻里看到薄书记就好象自己的父亲一样亲切而威严”。“老薄.您到哪里当官我就到哪里定居,跟您活的踏实”。<br />
<br />
网友呼吁薄熙来当主席总书记或总理<br />
<br />
一名在上海上学的大学生表示：“我觉得薄书记是我们学生敬重的书记，很多学生和我一样，看到有关薄书记的信息和镜头比看到大明星还兴奋。好书记！”“好一个打黑唱红的薄书记，他心里装着人民群众，想的是党的事业。这样的好干部理应重用，使全国人民受益！”“薄熙来好样的，人民有你这样的领导是福气！！到我们湖南来吧，这里太需要你这样的领导！！”<br />
<br />
网名还称呼薄熙来是“高瞻远足的政治家”，“希望薄书记当下一届的总理。看到了希望。我在大连四年，也是薄市长当家，很自豪。薄希来应该做总理。中国之大幸。百姓之大幸。<br />
<br />
薄书记是有魅力有能力的好书记，全国人民都喜欢你！”“为什么我喜欢沉浸于悲伤，唯有看到薄人，才向往快乐，薄熙来！！！中国的希望！！！”‘‘薄熙来，人民心目中的英雄。看到薄书记的新闻很兴奋！经济、政治、人品、品味和正气都不少，未来的总理”。</div>

							
							
							
							
