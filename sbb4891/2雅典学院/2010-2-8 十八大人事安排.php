&nbsp; &nbsp; 中共十八高度分权格局基本确立，「小常委」与「胡监国」体制将出现。国家主席与总书记两职分开从此形成定制，党内民主推进社会民主的设想将会化为泡影。 <br />
&nbsp; &nbsp; 中共十七届四中全会在神秘兮兮的气氛中结束，对外发布的公告既未涉及人事变动，亦未确言众人翘盼的官员家庭财产申报制度。此中，人事变动当以习近平晋补军委副主席为看点，但这个看点并未出现。分析人士认为：习的晋升将在国庆大阅兵后对外公布，或许在十八大时一次完成军权交接。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 不管如何测度，习近平接班的趋势已定。因为全会上是胡锦涛做报告，习近平就党建若干重大决定文件形成情况向全会作了说明。这一唱一和即意味着习近平接任党的最高职务已无悬念，但如何交接军权以及最高权力是否还延续「三位一体」则无法证实。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 缩水：常委九改五趋势明显<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 就近十几年的中共权力变动情况看，每次四中全会都要有重大人事变动。一九九四年的十四届四中全会，江泽民将黄菊拉进常委，同时增补吴邦国与姜春云为书记处书记，是为「以腐败换团结」的经典之作。一九九九年的十五届四中全会，胡锦涛晋补为军委副主席，正式对外释放「邓小平指定隔代接班人」的信号。二四年的十六届四中全会，胡从江手中正式接过军委主席一职，标志着「胡锦涛时代」的到来，也意味着党内派系斗争进一步复杂化。党内派系斗争的复杂化，促使中共内部寻求更加清晰的权力规则。此为「党内民主」口号的真实动因。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 党内权力斗争复杂化的直接表现是，政治局常委在十七大时仍保留了九个名额，而此前十六大的九名额被认为是各派妥协的结果，即权力分肥的权宜之计。按中共历史看，出现五常委的比率比较高，而且政治效率比较高，十七届政治局中共有七次。其余是：九常委制三次，六常委制两次，七常委制两次，三常委制两次，零常委（全部为政治局委员）一次。其中，六常委制出现于中共自称党内民主最好的八大时期。邓清理完左派而全面掌控最高权力的十二大亦是六常委制，意在重温八大之梦。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 九常委制始于文革后期的十大，毛的绝对强势自然消弱，叶剑英与朱德代表军方势力复起，「中间势力」董必武以垂老之身从政治局委员晋为常委。七常委制也是特殊情况下的产物，即赵紫阳被迫下台后，邓让江过渡而迅将朱F基与胡锦涛由中央委员直升为常委。因此，就出现了中共和平时期，胡锦涛连任四届常委的奇观。在中共八十八年的历史上，只有陈独秀、毛泽东与周恩来三人连任过四届常委，胡锦涛为第四个。这是个极其特殊的例子，因为无论如何，胡绝无陈毛周那样的创建之功。然而，这又为胡积累下超越邓与江的个人无形资产。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 十八大时，中共将被迫提高党内效率，常委「九改五」是基本态势。最大的变数，则是六常委制，出现七常委的可能性已经非常之小。因为，无论五或六常委，都利于胡锦涛以军委主席的身份监国。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 出列：三大员或将不在常委<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 十八大时，政治局常委改为五人制，其名额是：党的总书记一人、政府总理一人、党的纪检与政法书记各一人，备位接班人一人。简单地说，国家主席、人大委员长、政协主席三大员从常委中出列。该三职或由一般政治局委员出任。此种情况，在邓小平实控的十二大至十四大期间属于正常现象。如十二大时，政协主席邓颖超与委员长彭真是政治局委员而不是常委；十三大时，国家主席杨尚昆、委员长万里、政协主席李先念，前两人是政治局一般委员而无常委之职，而李则连政治局委员都卸任了。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 江泽民时期的三大员都以常委出任，旨在加强中共的全面控制，或言「消除自由化的后果」，云云。胡锦涛无力改变江泽民传递下来的局面，但常委职数过滥的后果已向他倾来万钧之压。政治运行效率低下，党内决策程序混乱，自上而下的系统化腐败，已将中共弄到政治破产的地步。如何摆脱江时代负资产，或言将邓遗产好的一面重新清理，就成了胡必须面对的现实。高调提党内民主，重点不在于如何反腐，关键是高层权力要分散化、决策过程要效率化。换言之，习近平到十八大时，不可能再「三位一体」，而是只任总书记，国家主席一职必须由他人担任，军委主席或将一分为二。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 推测而言：目前颇具竞争优势的薄熙来或可出任国家主席与政协主席两职之一，若是得选前者，则政协主席很可能由党外人士出任；江洋在十八大时进京亦成定局，很可能出任全国人大委员长一职。很明显，薄汪二人虽然进京，其党内职位则不会升迁，而由政治局一般委员身份出任政府、人大、政协职务。当然，薄汪二人「角色互换」也存在可能。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 监国：军委主席一分为二<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 习近平是否还经过军委副主席一阶已经不重要，即便他出任军委主席也只能是原来二合一职务的一种。现在所说的「中央军委主席」，实际上是中共军委主席与国家军委主席的合称。由于「党指挥枪」的强权原则，两职从来没有分离过。其实，在军队内部也颇有微词，不少高级将领希望两者分离或干脆取消国家军委主席一职。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 党内民主声起，高层分权里面军委主席一分为二，已经处于临界。难的是胡锦涛如何选择，才能实现十八大全程监国的计划。如果任党的军委主席，那只能让习近平以国家主席身份兼任国家军委主席，政治局常委职数就得「升六望七」，五人制无法实施。如果胡任国家的军委主席，监督权威就降了下来，但是他决心全面掌握武警的计划已经展开，似有该方面的准备。届时，或可由于各派妥协，胡留任合二为一的军委主席，起派系平衡器的作用。但是，如此而为又难免受讥，等于效法江当年的恋栈。不过，胡既敢以监国身份来掌控十八大这一过渡期，肯定不会如江那样半路交出军权。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 就目前的态势论，到十八时，习李留任实现双接班模式，政治局常委则很可能是：习李二人，令计划与王沪宁双进，备位接班的一人在胡春华与周强之间产生。习近平乐意接受胡的监国方案，因为他可以在十八大之后按胡的模式监国。而且，从此之后上任总书记或军委主席对下届实行监国将成为定例。按中共的行话说，叫「扶上马，再送一程」。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 民主：被吹气的大画饼<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 中共党内民主是个遥远的目标，至少在十八大一届不会出现大的改进，就不用说「以党内民主推动社会民主」之论了。仅从反腐护党一方面来看，总是说大卖小，既便在十七大届内公布官员家庭财产登记制度，操作起来也难免变异。因为无论党内民主之谓还是财产登记之施，都是中共自己闩上门办私事儿。社会监督不过稍作装饰，难有实质进展。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 十七届四中全会是中共在和平时期搞得最神秘的一次中央全会。开会期间，除了开幕时间报道外，绝无重要公开信息，有所涉及者均不足百字，绝不透露实质内容。会后的公报全是套话，「若干重大问题的决定」像个超计划生育的孩子不肯向社会亮相。此种明唱民主、暗捂信息的巨大矛盾正说明中共合法性危机的严重程度。恐怕会前为党内民主、财产登记鼓与呼的党建专家面对此状，会羞得抬不起头来。</div>

							
							
							
							
