90后男生：我无语了，居然真有人相信什么世博会花了4000亿，西南抗旱只拨了1.5亿，还有什么中国的教育投资不及非洲穷国乌干达。醒醒吧！盲从的校内人！90后的我带你们回归理性！！！<br />
<br />
最近一段时间以来，校内广泛流传着几篇针对中国社会、政府黑暗面的文章。诸如“不要再给西南灾区捐款了、玉树的地震和娱乐、地球上有一个很奇怪的国家、5.12纪念和今天有什么不同、韩寒谈世博《快来吧快走吧》、走吧走吧，都退出天朝吧、当今中国震惊世界的25个倒数第一&nbsp;&nbsp;”等等。这些文章所涉内容极为宽泛，但有一个共同特点，就是把现在的中国骂的一无是处，似乎已经改革开放了30年的中国还不如解放前的国民党统治时代。仔细读一下这些文章可谓是漏洞百出，有的甚至闹出了天大的笑话。比如最近流传甚广的国家对世博会及西南抗旱投资的对比，确实令人哭笑不得！<br />
<br />
&nbsp; &nbsp;在最初的“不要再给西南灾区捐款了”这篇文章中，署名是80后新贵韩寒。我不知道他是有内幕消息，还是亲自去核查过，总之公开渠道似乎无从考证，流传在网上的4000亿投资基本上可以认定为是“其”一家之言（不排除某人借韩寒炒作的可能）。那么开个世博会是否真花了那么多钱？根据公开报道，上海世博园区总共规划用地为5.28平方公里，也就是5280000平方米，如果你用4000亿去除一下5280000平方米，那就会得出一个极为咋舌的数字：平均每平方米须投资75757.58元！你用脚趾头想想，就算用全世界最好的草坪、最好的水泥，在世博园区内铺一块1平米的草坪、浇一块1平米的水泥路面也不会花掉近8万元吧？<br />
&nbsp; &nbsp;而事实上，上海世博会总投资为286亿，其中场馆建设180亿，运营费用106亿（上海市长韩正 语）。在运营费用中，光是社会赞助就超过70亿，这部分钱根本就不需要政府投资。于是，“韩寒”的惊天大荒就这样被拆穿了。。但必须要说明的是，正是由于这篇文章让很多人都在质疑我们是否有开世博会的必要，简单的说就是担心世博会能否收回成本。根据预测，在整个世博会期间（180天）平均每天要接待40万游客，最高峰期间甚至要超过60万！根据最近连续4次的世博试运行情况，要达到以上游客数量根本就不存在困难，甚至极有可能大大突破预测！我们来简单算一下，假设每位游客世博期间最低消费500元（门票+餐饮+住宿）计，每天40万的游客在世博期间光直接消费就超过360亿！远远超过世博会总投资！这还不包括由于乘数效应而带动的间接消费，各赞助企业由于世博会宣传而增加的营业额，世博会纪念品的销售收入等等。而上海乃至整个中国因为举办世博会在世界范围内城市形象、国家形象的提升更是一笔巨大的无法计算的无形财富！综上可知，举办上海世博会不仅在我国国力可承受范围之内，更是非常的有必要！&nbsp;&nbsp;<br />
&nbsp; &nbsp;再看一下，西南旱灾投资。中央首批抗旱资金确实只下拨了1.55亿，于是很多人起哄道：西南五省一市总人口加起来都不下1.55亿，平均下来每个人连买瓶矿泉水都不够。。咋一看，似乎说的很有道理，但显然很多人都忽略了两个极为重要的字眼：首批、受灾人数。实际上在整个抗灾期间，中央总共下拨了近15亿抗旱经费。何况是不是生活在西南五省一市的所有人都受旱灾困扰？显然不是！公开数据显示，西南旱灾总共受灾人口为2425万人。一方面是紧紧抓住1.55亿不放，一方面又无限的夸大受灾人口，写这些文章的人很好的找到了一个攻击政府的噱头，发出了“不要再给西南灾区捐款”的愚蠢号召，成功的愚昧了广大群众！某些人编织那么多谎言无非是为了把所有责任推给政府，自己不想履行作为社会成员应尽的一点责任也没必要找如此冠冕堂皇的理由吧？&nbsp;&nbsp;<br />
&nbsp; &nbsp;某些人也许又会说什么10多亿的抗旱投入也不算多啊。那就在嗦一点，抗旱不仅仅只依靠中央政府，更多的是地方政府。需要的不仅仅是资金，也包括人员、设备。据我所知，在整个抗旱期间，光军队就投入了26万人，他们的所有开销都不算在抗旱资金以内！而事实上，整个旱灾期间，光云南省的抗旱投入就超过了30亿！（其中中央下拨资金10亿、地方财政13.7亿、兄弟省份帮助及社会捐赠7.2亿）<br />
&nbsp; &nbsp;说到这，我不得不为英勇的人民军队说句公道话。在那些文章中，某些人公开批驳媒体对军队抗灾的高度赞扬，说什么这是军队的本职工作，说什么美国军人遇到灾难后也是冲锋在前，说什么从来不会有人因为干好了自己的本职工作就能获得别人感动，又以此联想到中国人的奴性云云。我不得不佩服某人丰富的想象力，还有惊人的无知。<br />
中美两国军队根本就不存在可比性！因为中国实行的是“义务兵役制”，也就是说每个中国军人都是发自内心的自愿参军、报效祖国，军人在大多数情况下并非是一种职业，因为他们只拿很少的津贴。美国实行的是“志愿兵役制”，说简单点军人在美国就是一种职业，拿着国家提供的高额薪水，替国家办事。按理说，只有在美国这样的国家军人救灾才是天经地义的，因为那是他们的职业。但就是在实行“义务兵役制”的中国，每当国家面临危难关头，冲在最前面的却总是人民子弟兵！从这个角度上说，解放军救灾作为一种高尚的行为本身就是不容任何质疑的！军人也是人，如果说美国军人干这些事是在间接的赚钱，那么中国军人救灾则在很大程度上是一种奉献！因为他们原可以把人生用于经商、深造，或者投身其他行业，用金钱来衡量的话，他们很有可能获得远高于军人津贴的报酬！诚然，同样作为军队，当国家面临危难时冲锋在前确实是其应有的责任，但如果把所有一切想成是理所当然，那就实在太没良心了！<br />
&nbsp; &nbsp;又比如，说什么中国的教育投资甚至不及非洲穷国乌干达，这更是滑天下之大稽！乌干达的年国内生产总值尚不到300亿美元，而公开的中国教育投资早已超过1200亿美元！我们的教育投入比乌干达的GDP都高，又何来中国教育投资不及非洲的乌干达？后来我才知道，原来在IMD（国际竞争力年度报告）2002年评价中，我国公共教育支出占GDP的份额很低，在49个参评的国家和地区中仅处在第47位，排在第46位的是乌干达！这下明白了，人家还真不是空穴来风，只不过把关键词改了，教育经费占GDP的比例被该成了教育投资，字面上变化不大，但最重要的概念被换掉了，差之毫厘谬之千里！我举个很简单的例子，一个每天只有50元收入的打工者，他把其中的20元用于吃饭（占比40%），而一个每天收入10000元的大老板，他每天花100元吃饭（占收入比重为1%），你能说这个打工者过着比老板更优越的生活吗？扯淡！<br />
&nbsp; &nbsp;还有，说什么中国排挤外资，把谷歌退出中国市场说成是遭中国政府驱逐。如果说以上内容是因为知道的东西不全而作出错误的判读尚可理解，但对谷歌等外资企业的打抱不平则一定是居心叵测！君不见谷歌是因为违反中国法规，并且据不悔改后“自愿”退出中国的吗？君不见现在中国笔记本市场上销量最高的是惠普、苹果、东芝、索尼，手机是诺基亚、摩托罗拉、三星，汽车是通用、福特、丰田、现代、标致吗？君不见各大城市沃尔玛、家乐福、肯德基、麦当劳到处林立？君不见美国人三天两头拿中国汇率说事，隔三差五地对中国产品发起反倾销调查吗？君不见在中国出口企业200强中，外资企业占了153家，出口额占了77%吗？人家在中国经营违反了中国法律，自愿卷铺盖走人，却总有人出来为其打抱不平、鸣冤叫屈，这搓人到底是人家的间谍还是中国人自己养的走狗？<br />
&nbsp; &nbsp;又有人说中国政府控制传媒，愚昧大众。我告诉你，每个国家都是这样！就在那些人极力宣扬美国的民主自由、羡慕其经济发达、社会保障优越时，人家早已把中国妖魔化！你以为西方国家就会让国民知道真实的中国？在大多数西方人的眼中，中国依然是一个贫穷、落后、专制独裁的社会，他们对中国的思维方式依旧停留在毛泽东时代！这当然应归功于西方媒体对国民的思想灌输！你以为美国人能公选总统就很民主？奥巴马在被党魁推举为民主党总统候选人之前，有多少人认识这个其貌不扬的黑人？他在竞选期间花掉的美元估计可以装满你家一个房间！这种建立的金钱上的选举难道就是所谓民主？你觉得在伊拉克、阿富汗等等被战争打的千疮百孔的国家，在他们的人民食不果腹的情况下摆个选举箱，让他们自由选举统治自己的总统有意义吗？没有一定的经济基础，建立所谓的民主社会根本就是扯淡！<br />
&nbsp; &nbsp;还有人抱怨中国的土地国有制度，说实话这种抱怨实际上也就意味着对整个社会主义制度的质疑。抛开社会主义国家一切生产资料归国家所有等大道理不讲，假如中国真如西方资本主义国家一样实行土地私有制，允许自由买卖，那么必然引起大规模的土地兼并，造成“富者有弥望之田，穷着无立锥之地”！因为中国2000多年的封建制度就是一部血泪的土地兼并史！这样的后果造成的贫富差距将比现在的更为巨大！这一点在现在的印度表现的极为明显，印度拥有比中国多得多的耕地资源，在人口还中国多的情况下，其人均粮食产量却不到中国的一半，但印度的顶级富豪却比中国要多得多！这也直接拆穿了某些人所谓的印度60%以上的人口生活水平高于中国的谎言！<br />
&nbsp; &nbsp;还有人嘲笑中国，说中国作为联合国五个常任理事国、五个核大国之一，但却是尖端武器的净进口国。这种说法本来就是荒谬的。五个核大国中，英国海军的战略导弹就是美军的“三叉戟”；法国的“戴高乐”航母上停着从美国进口的预警机；美英法三国的军舰上装的反舰导弹，不是法国的“飞鱼”，就是美国的“鱼叉”，防空导弹不是美国的“标准”系列、就是法国的“紫菀”；就连美国人吹嘘的很牛B的“战斧”巡航导弹上，最重要的超导陶瓷部件进口于日本；尼米兹级航母上还装配着英国的“海麻雀”舰空导弹；E―2T预警机甚至借用了部分以色列的技术；就连向来不服软的俄罗斯也想购买法国的“西北风”级两栖登陆舰。。发达国家之间尚有武器出口贸易，更何况是中国？每个国家都有自己最擅长的技术领域，根本就没有必要在所有领域都自主研发。为了共同的利益，相互出口帮助本来就是很正常的事。再说，中国在进口的“苏―27”后不久就搞出了自己的“歼―11”，进口了“S―300”后不久就搞出了自己的“红旗―9”，进口了“龙卷风”后不久就搞出了自己的“卫士―2”。。这种强大的吸收再创新能力让中国少走了许多弯路，极大的缩短了国防现代化进程，这样的好事有何不妥之处？<br />
&nbsp; &nbsp;更有人居然质疑“抗美援朝”战争的必要性，说什么正是中国扶持了金家王朝，才使得朝鲜人民到现在都生活在水生火热之中；说什么抗美援朝让西方国家开始了对中国30多年的包围封锁，使中国错过了最好的发展机遇等等。说这些话的人我不知道该是唾骂还是哀叹其无知！在当时的情况下，假如中国不出兵援朝，那么北朝鲜必然亡国，今天的朝鲜半岛就将是在亲美的韩国统治之下，美国军队即可饮马鸭绿江畔，美军的火力将覆盖整个中国东北地区，美国人的坦克有能力在半个月内兵临北京城下！“唇亡则齿寒、户破则堂危”，这种最简单的道理那群人居然都忘了！退一万步说，就算中国不出兵朝鲜，难道美国就不会包围封锁中国了？亏的这群人能幻想的出来！这种质疑本身就是对革命先烈的亵渎与侮辱，说这种话的人根本就不配活在这个世上！<br />
&nbsp; &nbsp;还有人依据中美两国签订的只是《停战条约》并非《投降条约》而替美国人辩解，说中国根本就没有赢得那场战争。那我反问一句：朝鲜战争起始点是在哪里？鸭绿江；停战点是在哪里？三八线；那么是谁的参战将美国人打回了三八线，甚至一度丢掉了汉城？又是谁将由17个国家组成的联合国军打成了残废？又是谁逼得美国人不得不在《停战协定》上签字议和？是光荣的中国人民解放军！在军事实力、工业实力、科技实力远远领先于中国的情况下，美国人却被打回了原处，不是美国输了，难道还是中国？<br />
&nbsp; &nbsp;中国是偶尔需要进口武器装备，但中国的国土起码不像某些所谓发达国家需要别人来防守！中国政府在很多方面确实不如人意，但中国起码让国民享受了30多年的和平生活！中国的贫富差距确实在不断扩大，但人民生活水平不断提高却是不争的事实！中国人民确实中庸保守，但正是这个充满奴性的民族却创造了延续5000年的中华文化，从未间断！中国确实还很落后，但正是这个古老落后中国却让某些人可以吃饱了撑着将养育他的祖国骂的一文不值！<br />
&nbsp; &nbsp;醒醒吧！盲从的校内人！90后的我带你们回归理性！！！<br />
