													<h2>暗杀一个公安副局长就是这样</h2>
						
						
																											<div id="postmessage_663138" class="t_msgfont">这两天疯传一个新闻：“黑龙江佳木斯警方昨日证实，同江市公安局副局长孙巍3月28日晚7时30分许被发现在自家车内遭枪杀。当地人士透露，孙巍家住同江农业银行家属区，遇害时被枪杀在自家车内，周边有居民曾听到枪声。据悉，孙巍曾任同江市公安局副局长，主抓刑侦，近期升任同江市公安局政委。”<br />
<br />
你们看，干掉一个公安局的副局长，就是这么简单，出其不意，易如反掌！<br />
<br />
这个事件，若是革命党干的就好了，但目前看来仍然不是革命党，因为没有哪一个党派组织发布申明说对此事负责，没有扩大此事的影响。可是，对我们在谋求革命的人来说，这个枪杀事件却在多方面给我们启示。<br />
<br />
1. 首先说明，现在这个时代，从事暗杀是完全可行的。共匪政权是专制复辟，既然我们说“推翻伪共和，建立第二民国”，那么，辛亥革命就值得好好研究。辛亥革命中，暗杀发挥了巨大作用，暗杀清廷要员的秘密组织，层出不穷，孙中山先生毫不讳言秘密召募革命敢死之士刺杀摄政王。暗杀震慑了清廷，瓦解了其斗志，号召了革命。现代，共匪控制非常严密，革命之初，唯暗杀一路可行，也必可起到震慑共匪，以恐慌瓦解共匪，号召革命的大作用。即使不能暗杀政治局常委级的共匪魔头，暗杀地县级的恶霸共匪官僚及其爪牙，则完全可行。以严密的组织，精心的策划，果断的实施，彼共匪活动于明处，我活动于暗处，如幽灵一般，可以轻易得手。现代社会条件，一切都在双方同时发生着变化。共匪虽然严密控制，但秘密革命的活动者，却也同样可以伸展出许多活动空间，有便利的交通，强大的通信，四处可得的合成强力炸药的化工产品，火力强大的枪支，你用你的，我用我的。人是灵活的，双方都可以用智慧应变，适应，进化，要严密控制灵活的人，必定失败，而反抗必定成功。重庆抢枪至今未破，非常好。<br />
<br />
2. 其次说明，现在这个时代，暗杀的影响力是巨大的。人民对共匪恨之入骨，已经对共匪动了杀机。试看这个枪杀事件发生后，网友疯传，一片叫好，就知道它是多么大快人心。如果这是革命党干的，只要在网上发布暗杀现场照片，宣布是某某党对某共匪官僚执行死刑，那么，其影响又该是何其大呢，对革命的宣传又该是何其大呢，对于鼓舞民众投入暗杀共匪的活动，作用又是何其的大呢？<br />
<br />
3. 革命思想的宣传还很不够。如果革命思想宣传的很充分，大多数人都知道有这么一种暗杀共匪的革命思想，那么，就是一起小小的报复杀官，也会自觉不自觉地打出革命的旗帜，其对革命的进一步推动作用又该是多大呢。等到全民皆知道这一思想，都接受这一思想，都以暗杀共匪为荣的时候，就是陷共匪于全民暗杀大潮的时候。那时，共匪就必定会象日伪时代那样，只能建筑碉堡来办公，离全盘解体也就不远了。<br />
<br />
4. 民众之中，蕴藏着巨大的复仇力量。民间，人民，对共匪的报复欲望，与日剧增。枪杀同江公安副局长，不管这件事是什么性质的凶杀，都充分说明，人民之中对共匪的杀机，已经萌动。现在民众之所以还没有大规模暗杀共匪官僚，只是因为“恐惧”二字，找不到出路。不能害怕共匪，越怕它越猖狂。如果你害怕共匪，共匪则会利用你的害怕，如果你打破了恐惧，反过来就是共匪被诛杀的时刻。怎样打破恐惧呢？将恐惧转化为大愤怒，就能克服恐惧。对共匪革命，是赤裸裸的战争，任何战士，上战场之初，都是恐惧的，但后来的恐惧，都化为了愤怒，一旦愤怒，内心之中，恐惧就荡然无存，只想着复仇杀敌。现在，人民对共匪的恐惧，开始逐渐打破。重庆抢枪，同江诛暴，杨佳杀警，共匪制造的什么“神圣不可侵犯”的谎言、假象、恐吓，逐渐破产了。任何一起和谐事件，对维权者的镇压、殴打、关黑监狱，都在推进民间的愤怒。革命者就是以对共匪的刻骨之大仇恨、大愤怒，克服内心的生死之大恐惧，对一切共匪展开凌厉不可阻挡的疯狂的杀戮。当代革命党人，只需要释放和启动民众中这股大复仇、大愤怒的力量，那么对共匪的报仇狂杀之高潮，就会应声而来。对共匪官僚及其一切家属，那些同饮民众之膏血的附罪之人，进行大面积的暗杀，就无人再敢当共匪的官，共匪就必然解体。灭共由我不由天，不能指望“天灭中共”，让人民报仇，报仇，这才是灭共的终极力量。</div>

							
							
							
							
