																											<div id="postmessage_166400" class="t_msgfont">我上小学的时候，班里有一个有些女性化的男同学，他所到之处都会遭到许多一脸无耻模样的学生的嘲笑，甚至在这些嘲笑的行列中时而还会出现人民教师的身影。当然，在当时那个环境中，我也不能够免俗，和众多无耻的人站在了一起，当着面冷嘲热讽或者背地里将其当作无聊的笑柄，最终成为了无耻者中的一员。<br />
<br />
记得那位被众人嘲笑的男同学总是一副内敛羞涩的样子，在一群无耻者里完全找不着归属感，但他越是这样，我们便越是变本加厉的嘲笑他，大声喊他假小子或者假妮儿。现在我想起过去的这些事情，感觉自己当年简直就是一只传说中的傻逼。<br />
<br />
小时候的事情总是不容易那么解释清楚，因为按我的理解，小孩子其实和白痴没有什么本质上的区别，白痴也就是成年后的小孩子，两者的区别是年龄，共性是没有应有的理智。但幸好我们大部分人都会从小孩子过度为一名正常的成年人。<br />
<br />
等到我变成一个被社会所定义的成年人之后，小时候的干过的许多荒唐事情就被用一些词语给简单的概括掉了，比如童言无忌或者是天真无邪之类，大家在这些词语面前对自己小时候的无耻行为做了赦免。<br />
<br />
 <br />
<br />
作为一个成年人，我觉得以嘲笑一个人的相貌，或者是从某人的相貌中引申出来一些以引人发笑或者排遣空虚为目的的做法，是极度低智的一种娱乐方式，毫无幽默感和创造力，这不是童心未泯，完全是小孩子的无聊白痴举动。但是最近在网络上就出现了这样的事情，那就是不知道笑点究竟在哪里的“春哥”，难道是仅仅因为李宇春那张有些男性化的脸以及身材吗？如果真是源于此，我的胃液已经在止不住的翻滚了，下一步就是从嘴里喷薄而出。<br />
<br />
根据知名博客以及意见领袖和菜头的解释，这是一种传说中的“网络亚文化现象”，并且还非常饶有兴趣的分析并列举了一些事例。但我所有的感觉汇成一句话，那就是很无聊。难道这就是“春哥”以及“春哥教”的终极意义吗？在以前的网络流行语“打酱油”，“俯卧撑”或者是“躲猫猫”中，我还能或多或少的心领神会，而且这些流行语背后都有一段并不无聊甚至很精彩的故事，能让我和其他人达成一种共识，了解这些流行语的深层次含义。但“春哥”却让我百思不得其解，迷茫的要命，还是那句话，是我out了还是这个世界因为太无聊而疯掉了。<br />
<br />
 <br />
<br />
