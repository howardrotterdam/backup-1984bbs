　　由于中国文化不鼓励敢于冒险这种优良品质，所以中国人极力避免冒险，他们也不想寻求机会来改善自己的生活。中国人对于生活的平衡性和意义性并不感兴趣，相反他们更执迷于对物质的索取，这点上要远远胜于西方人。大多数中国人发现他们不懂得&quot;精神灵性&quot;，&quot;自由信仰&quot;以及&quot;心智健康&quot;这样的概念，因为他们的思想尚不能达到一个生命（补：即肉体和灵性的并存）存在的更高层次。他们的思想还停留在专注于动物本能对性和食物那点贪婪可怜的欲望上。<br />
　　在中国人的眼中，受教育不是为了寻求真理或者改善生活质量，而只是身份和显赫地位的象征和标志。中国的知识分子从别人那里得到尊敬并不是因为他们为了别人的幸福做过什么，而只是因为他们获得占有相当的知识。事实上，他们中的大多数只不过是一群仅仅通晓考试却从不关心真理和道德的食客。<br />
　　中国的教育体系很大程度上已经成为一种失败和耻辱。它已经不能够服务于教育本应所服务的对象：社会。这个教育体系不能提供给社会许多有用的个体。它只是制造出一群投机分子，他们渴望能够受益于社会所提供的好处却毫不关心回报。<br />
　　中国可以培养出大批的高级能人才，但却很少可以培养出合格的可以独立主持的管理级专家。服务于一个公司或者社会，光有技术是不够的；还需要有勇气，胆量，正直和诚实的领导才能，这恰恰是大多数中国人所缺少的品性。正如亚瑟.史密斯，一位著名的西方传教士一个世纪所指出的，中国人最缺乏的不是智慧，而是勇气和正直的纯正品性。这个评价，虽然历经百年，如今依旧准确诊断出中国综合症的病因。<br />
　　大多数中国毕业生对选择出国并为外国工作不会感到内疚，事实上他们首先欠下了中国人民在教育上为他们所做出的牺牲。随着传统文化价值观的破坏和逐步衰弱，大多数的中国人，包括受过教育的人都徘徊在精神和内心世界的路口，像迷失的狗一样不知何去何从。</div>

							
							
							
							
