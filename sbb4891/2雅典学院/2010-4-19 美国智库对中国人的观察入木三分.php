&nbsp; &nbsp;&nbsp;&nbsp;背景介绍<br />
&nbsp; &nbsp;&nbsp;&nbsp;美国兰德公司是一家著名的非盈利的研究机构，为美国方提供“客观的分析和有效的解决方案”。最近，他们公布了一份对中国现状分析报告，即有肯定，也有严厉批评，值得国人反省。本文观点来自兰德公司亚太政策中心。<br />
　　<br />
&nbsp; &nbsp;如果20世纪的中国是一个富裕和统一的国家，我们会有一个完全不同的第一次世界大战，我们就不会有第二次世界大战而是第二次欧洲大战。中国能够阻止日本侵略或者打败日本。美国在这些冲突上的花费从根本意义上会减少很多，因为珍珠港事件不会发生。我们和整个世界，更不用说10亿中国人，一个多世纪以来，已经为中国的弱小付出了惨重的代价。世界需要一个健康的中国。<br />
　　中国的需求对日本走出衰退起到了促进作用。日本状况给世界经济带来了风险。关于这一点，怎么说都不夸张。日本巨额的债务会产生多米诺骨牌效应，逐渐波及到全世界。在中国有力的帮助下，危险似乎已经过去。中国全球化给美国带来了很多影响。最明显的是，中国成为美国商品最大的市场。<br />
　　可口可乐早就完成了那个看上去像是神话的目标：卖10亿瓶可口可乐；曾经嘲笑中国梦的通用在中国卖了很多的别克汽车，在困难时期，中国带来的利润占通用利润的很大一部分；中国联想购买IBM个人电脑业务，挽救了这个垂死部门的工作岗位。中国提供更低价的生活必需品给美国人的生活水平做出了很大的贡献，尤其是对我们不是那么富裕的居民而言。有迹象表明由于能够购买中国低价的出口货物，低收入美国人的生活水平可能提高了5%到10%。<br />
