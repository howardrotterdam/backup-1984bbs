													<h2>Google不惜以退场来争取中国网民的人权？……别开玩笑了！</h2>
						
						
																											<div id="postmessage_431303" class="t_msgfont"><a href="http://semwatch.org/2010/01/allaboutlogics" target="_blank">http://semwatch.org/2010/01/allaboutlogics</a><br />
<br />
我国古代民间文学的光辉诗篇《孔雀东南飞》讲述了这样一个故事：低级公务员焦仲卿迫于老母的淫威休了美丽贤慧的妻子刘兰芝，在刘兰芝面对再婚压力举身赴清池之后，焦仲卿也后悔地自挂东南枝了。<br />
<br />
中学里的这堂课很热闹，语文老师让同学们积极发表看法，广大同学异口同声地表达了对老母的鄙视和厌恶。当时我对南北朝时期的家庭结构认识不足（现在也不足），很想不通：明明是焦仲卿休的刘兰芝，为啥同学们不批评焦仲卿呢？这个问题提出来，冷场几秒钟后有人说：焦仲卿都自杀了……<br />
<br />
关于Google可能退出中国这样的举动，搜索市场所受的影响虽然对于这个博客的作者们和读者们来说很大，但在整幅画面中终究是小小小小的一块。所以昨天发的贴子是基于一时激愤的纯八卦。今天衍生出来的讨论倒是和搜索营销相关了，因为这里要谈的是批判性思维。<br />
<br />
我之前工作的公司也算是业内的一个大腕，但进入中国市场的道路并不平坦。有人对我说过，你们这个产品先进是先进，但也未必就符合中国市场的需求。我说：这样下结论是不是早了点，都没还怎么进来呢！他说，你看看，除了XX，其他账户不是表现很糟糕吗？无语了一会儿，我给他举了一个例子。<br />
<br />
我说差不多98，99年的时候我也曾是一个可耻的文青，喜欢混摄影论坛。那个时候没有现在这样百花齐放，我去一个江湖绝色，还去一个色影无忌。江湖是技术坛，无忌是器材坛。通过在无忌坛的成长，我的相机从珠江单反升到了哈苏单反，但是反而不在江湖发帖了。器材升到顶级以后还拍不好，那是我的问题。<br />
<br />
除了XX之外其他账户的表现不好，是否足以说明这个产品并不适合中国市场？我觉得还不够。因为除了XX和XXXX，中国市场的其他账户都是在二级代理商手里运作的。二级代理商在乎的是变现，而非账户表现。拿来一个合同就直接把工具丢到账户里，连最起码的环境优化都不知道实现，又怎么能得到好的效果呢？工具毕竟是工具，是管理者来用的。如果管理者自己头脑不清楚，再好的工具也是白搭啊！<br />
<br />
他就追问：所以你觉得这个工具也是适合中国市场的其他行业的？<br />
<br />
我无可奈何地说：我也不知道……你要是听明白我的意思就不会这么问了。没有得到有效的检验，怎么去证明这个工具到底是不是适合中国市场？我只是认为你到达结论太快了一点。<br />
<br />
在数字化的营销管理工作中有两个难点，一个是数据占有，一个是数据处理。由于技术手段相对落后，我接触到的相当一部分在线营销管理员都认为数据占有是一个主要难点。“如果我能够获得什么什么的数据，我就可以如何如何了。”这个看法的前半段是正确的，后半段则未必。是否可以如何如何依赖于你的数据处理能力。<br />
<br />
对于那位质疑自动化产品效果的朋友，我没有说出口的是：以他的思维方式，目前不可能是一个合格的数字营销管理者。先有结论，再去挑数据支持，这是艾瑞报告的做法。<br />
<br />
用谷歌门引发的昨天这个帖子来讨论一下数据占有和处理两个方面。MONEY在之前的跟帖中提到信息不对称，这是一个非常重要的因素。我的判断是建立在我所获得的信息基础上的，当然未必正确，起码其来有自。<br />
<br />
&nbsp; &nbsp;1. 在Google发布博文之前，白宫与几个高科技公司的大佬进行了密谈，提到了把审查制度作为贸易壁垒的一个部分来进行对待，认为审查制度是自由贸易的一大害。<br />
&nbsp; &nbsp;2. Google发布博文，以谷歌中国的存亡为弹药向北京政府进行了“自杀式的攻击”。<br />
&nbsp; &nbsp;3. 美国国务卿希拉里立即对Google博文作出反应，要求中方对黑客攻击予以解释。（国务卿出面谈一个商业公司遭受的黑客攻击，这个事情以前发生过吗？我不是很清楚，我记得以前的反应好像是针对红网攻击的，性质完全不同。）<br />
&nbsp; &nbsp;4. 路透社报道，希拉里将于下周宣布一项技术政策，以帮助其他国家的公民获得未经审查的网络资源。<br />
<br />
我想国内相当一部分网友并没有知道上述全部内容，这是信息获取不对称的问题，因此形成不同看法是非常容易理解的。但在国内网友可以占有的信息上，是否存在处理方式的差异呢？<br />
<br />
谷歌门的核心问题在这里：审查制度好不好？我认为不好。 Google的产品好不好？我认为非常好。Google坚持信息自由的立场对不对？这个问题有一点点埋伏，但总体上我认为是对的。Google为中国人民不能自由获取信息感到愤怒，不惜以退场来争取中国网民的人权？……别开玩笑了！<br />
<br />
批判性思维是米国高中生和大学新生的一门必修课，不过必修课的意思就是说凡是必须的，就是广大学生最学不进去的，所以受过高等教育的米国人民在表现脑残的时候并不比文盲强多少。批判性思维这个词很拗口，其实通俗点说就是逻辑。看几个谷歌门里的逻辑谬误：<br />
后此谬误<br />
<br />
常见的逻辑谬误中有一种叫后此谬误。简单说就是A先于B发生，所以A导致了B。A当然有可能导致B，也完全可能和B无关。谷歌门事件中，审查制度是 A，Google威胁退出是B。Google说：TG你坏，我就不审查，你要干涉我我就退出。所以顺理成章的，审查制度成了Google退出的原因。那么，我们是否应该相信Google的说法呢？3年前在面对国会质询的时候，Google可不是这么说的。那时候Google义正辞严（一如今日）地说，我们在某个国家做生意，当然要遵循该国的法律…… 当然你也可以说这3年来Google饱受凌辱，心理承受到达了边界，忍无可忍终于爆发。所以价值观是有临界值的，过了这个值就会掉头。这个观点我没法争辩，因为对价值观的定义都不同了，鸡同鸭讲。<br />
红鲱鱼谬误<br />
<br />
红鲱鱼谬误的来源据说是越狱犯到处乱丢红鲱鱼，用来干扰警犬的鼻子。我不知道到底有多少人看了Google博客的原文。原文之所以引发争论，因为 Google给出的最直接的理由是受到黑客攻击。Google突然拿出一桩黑客攻击的例子来说事，并暗示黑客是中国政府组织的行为，进而跳跃到了“审查多年还侵犯人权，我不玩啦！” 对于大型网站来说，受到黑客攻击不是新闻，不受到黑客攻击才是。Google受到黑客攻击的这个案例我是完全相信的，从好大一串攻击中挑出一件来支持人权自由论，就不能令我信服了。好好说事，别乱抓红鲱鱼！<br />
稻草人谬误<br />
<br />
如昨天所说，对于Google选择的举动，我觉得是合理的，对于它的价值观，哪怕前后并不一致，我也尊重。让我有写帖子冲动的，主要还是一些网友的反应。Google有非常好的产品，如果它离去，给中国用户带来的损失是巨大的。但认为Google离去损害了中国网民的言论自由，就是完全无稽的说法。 Google.cn在2006年发轫之际受到美国国会质询，是因为它遵从政府要求进行了自我阉割。它给用户带来的体验一直是过滤过的，局域网一直都是局域网。本来没有用户从Google.cn上享受过自由，我不理解为什么它的离去就损害了这种没有存在过的自由？稻草人谬误的意思是说，我做个稻草人拿针扎它，真人是不是会疼我就不管了。<br />
乐队花车谬误<br />
<br />
最可怕的谬误是乐队花车，或者说从众谬误。因为别人或者多数人相信什么，就去相信什么，这是从众效应。在中国互联网上从来不缺少干柴，划一根火柴就能呼啦烧个山摇地动。Google要离去是为了为广大P民出头，这是多么让人热血沸腾的事情啊！大家都这么说，群众的眼睛是雪亮的!当信息来源是大众传媒的时候，群众的眼睛从来不是雪亮的。这个时代的新闻媒体不过是大众娱乐的载体，要带给大家的，不管是欢乐还是愤怒，都不过是廉价的娱乐罢了！在这种群体性激动中，事件的具体起因并不重要。归根结底，是长期以来的舆论压制造成Google的破釜沉舟，都是阿共仔的阴MIU啦！群体性激动是简单的站队游戏。当有一个伟大光明正确的Google站在群众这边，为了自由和尊严做出重大的抉择，这个站队游戏就狂欢节化了。这自由和尊严到底是谁的，再说！站完队以后怎么办，再说！群众的热情是被类固醇驱动的，但在Google那一方，驱动行为的是平衡和计算……<br />
<br />
这些逻辑谬误所关联的信息是国内网友可以获取的，是数据处理的问题，不再是数据获取的差异。我相信这个博客的大部分阅读者和数字营销有相当的联系，那么这个数据处理的能力对作者对读者同样重要。<br />
<br />
我也是活在Google上的人，很乐意看见读者用你们的观点和理论把我打倒然后踩上一千只脚，让我相信Google真的是一个道德楷模。但是但是，请不要仅仅诉诸情感，请用逻辑来说话。<br />
<br />
我念中学的时候曾经想过，如果焦仲卿居然胆敢不上吊，那么可供批判的对象就成了母子二人，尽管在休妻这个事实上并无改变。<br />
<br />
眼下又有了别开生面的可能：这个焦仲卿如果是个强力人物，面对封建压力居然敢于揭竿而起，老娘我不要了，老婆我也不要了，从此叛出家门和海藻过上了自由快乐随心所欲的生活，那么广大群众就应该感动得热泪盈眶狂赞够MAN！刘兰芝还是继续去赴清池好了。</div>

							
							
							
							
