													<h2>房子让80后成为垮掉的一代</h2>
						
						
																											<div id="postmessage_174350" class="t_msgfont">不知道从什么时候开始，70后已经退居幕后，而80年后逐渐成为社会舞台中央的绝对宠儿。80后的作家、CEO、亿万富翁等等一系列令人羡慕的光环笼罩在他们身上。80年同时也成为社会消费的主力军，正为各行业商家的争抢对象。<br />
<br />
80年后成为楼市的主力军，也应运而生成为楼市的流行语。但是在流行语的背后，却透露出“宠儿”们的心酸和无奈。作为时代的幸运者，80后可谓如鱼得水，有知识、有远见，更难能可贵的是有一种独立自主生存精神。但是因为房子的存在，我们最好的一代却生不逢时，因为他们遭遇到了高房价。<br />
<br />
如果没有高房价，如果我们可以用3~5年的收入买套房，那么80年后的生活将会轻松很多。但实际情况是，如今的大部分人不吃不喝需要大概15~20年的时间才能买套房。因为房子，我们害怕事业；因为房子，我们不敢创业；因为房子，我们被迫啃老；因为房子，我们不敢养育下一代。<br />
<br />
这就是作为我们社会栋梁80后们的真实写照。记得不少开发商的御用文人在媒体上大谈房子对于青年的生活幸福度的重要作用。我想这是当代中国市场经济体系中的最大谎言。比文物价格还贵的房子如今能给年轻人带来的只有痛苦和压迫下的无奈。<br />
<br />
一个幸福的社会应该是一个多样化的社会。年轻人不仅要拥有房子，更应该拥有其他幸福的一切，比如家庭的天伦之乐、夫妻恩爱之乐、相夫教子之乐。但是因为有了房子，其他的幸福就不复存在，而且房屋本身和乐只有？当80后每天一睁眼就是数百元的房贷，他们还有心情去想念其他吗？<br />
<br />
记得有一个北京的出租车司机大哥的话让我震动良久。当我问他整天跑北京的大街小巷烦不烦时，这位大哥回答我：“烦也没有办法，每天一睁眼，就是300元的车份子钱，你不跑怎么办？我也想周六带老婆孩子出去玩玩，但是人穷志短啊。”<br />
<br />
好一个人穷志短，一语惊醒梦中人。这话对于80后同样适应，最有前途、最有潜质让中国强大的一代人如今因为高房价而沦落成为最志短的人。可以毫不夸张的说，80后因为房地产而沦落为最没有志气的一代。<br />
<br />
我认识一位北大毕业的80后，如今毕业5年，但从未见父母来京和其一同生活过。平时一起聊天问其原因，仁兄的回答是：“父母不愿来京，因为怕增加其负担。我也不愿意让他们过来，等我买了房子再说，没有房子在父母面前多没有面子。”<br />
<br />
听了这话，内心真的是万分凄凉。如果在20年前的今天，你问一个北大学子的抱负，他一定会回答你“我将为祖国强大、为民主而奋斗终生”；如果你问10年前的北大学生，他会回答：“我将成立中国的微软公司，一定会超越世界首付比尔盖茨”。今天的北大学子，其抱负却转变成为尽早拥有自己在北京的房子，这样父母来京自己就不会太濉<br />
<br />
抱负的转变不仅反映80年后生存的尴尬，更折射出我们这个国家不但没有进步，相反却一步一步将年轻人逼向堕落的深渊，这是一种怎样的悲哀和无奈。当我们骄傲的宣称GDP进入世界前三、我们的航天员实行太空漫步时，我们的房价正和国际接轨时。殊不知我们丢失了一国发展壮大的最核心竞争力，那就是青年一代的创造力和发展力。当80后人人为房子奋斗终生时，这个国家只会生产房奴。</div>

							
							
							
							
