													<h2>胡锦涛悄然回归以阶级斗争为纲治国</h2>
						
						
																											<div id="postmessage_366608" class="t_msgfont">今年二月，中共中央下发了二号文件，该文件充满阶级斗争的腾腾杀气，恍若毛共王朝再现，令人有今昔是何年之感。整个文件凝聚着一种意志，即不惜一切代价保专制。 <br />
<br />
人们普遍没有觉察到：这是又一个路标转换的重大标志；中共胡锦涛中央二号文件的出台，正式标志着中共已经放弃以经济建设为中心的邓小平路线，重新回归“以阶级斗争为纲”的治国路线。只不过这次阶级斗争，不是毛时代中共毛系当权派对党内“走资派”、对地富反坏右及其子女进行专政的“阶级斗争”，而是中共法西斯新贵“走资派”集团对劳动人民及弱势群体进行专政的阶级斗争。 <br />
<br />
但许多人不以为然，认为二号文件不过是中共为了应对2009年这一“政治敏感年”而采取的临时性措施。我要提醒这些人：去年以确保奥运会名义采取的管制措施，大多数已经保留下来，比如：在北京实施的全面监控外地入京手机号码、禁租法轮功信徒和“不稳定分子”等租房管制、在全国范围实施的互联网封锁过滤的升级、对公共场所文娱活动的严限等极权措施。这些足以证明：“确保奥运”等所谓临时需要的幌子，不过是中共胡中央厉行倒退的堂皇借口。 <br />
<br />
实际上，从2007十七大开始到现在，中共以临时性需要为由头而采取的诸多倒退措施一直有紧无松、有进无退、步步为营，从没因为临时性的由头消失而取消、放松过，这一点和邓江时期大相径庭，邓江时期，每逢“两会”、“六四”忌日，中共也会收紧管制，但远没有收得象今天这样紧，而且，一旦“敏感日”过后，包括新闻在内的管制一定会放松，从没有现在这种一再“紧套”的情形。 <br />
<br />
自2007年十一月以来，“确保十七大”、“确保奥运”、“确保政治敏感年”...这些步步为营的倒退举措，隐隐闪现着长远的图谋，如幽暗中剧毒的大蛇一般无声地伸缩着有毒的信子。 <br />
<br />
胡锦涛中央倒退举措的非临时性，还可以通过对比一窥其端倪。胡锦涛当前的极权管制程度，远远超过了1999年，1999年是“六四”大屠杀十周年、达赖出亡四十周年、拉萨大屠杀十周年...同样是十足的“政治敏感年”。胡锦涛当前的极权管制程度，实际上超过了文革时期，倒退回毛共“十七年”的年代；也就是说，胡锦涛的高压举措远远地超过了维稳的需要。 <br />
<br />
近七年来，在胡锦涛“温水煮青蛙”般的倒退政策中，越来越多的人已经见怪不怪，他们无所作为地期待明天来临，殊不知到惊觉灼烫之痛的那个时刻，已经无力跳出烹煮之釜了。 <br />
<br />
“温水煮青蛙”，杀人不觉死，这就是胡锦涛比江泽民、邓小平更为可怕的地方。那么，胡锦涛一伙到底想干什么？他要把中国这只青蛙煮成什么样的菜肴？一切迹象表明：胡锦涛一伙正在切切实实地重操以阶级斗争为纲的统治法术，企图以高压恐怖吓阻中国民众的自由民主诉求，死保一党专制到底。 <br />
<br />
胡锦涛所签发的二号文件，核心精神是不惜代价“维稳”，也就是说，为了维护一党专制的稳定，终止经济发展也在所不惜，这实际上是宣布终止以经济建设为中心。从自2007中共胡政权温水煮青蛙的倒退趋向来看，这决非权宜之计，而是一项战略性的政策调整；而以经济建设为中心是邓小平（包括江泽民）路线的基石，终止以经济建设为中心，也就是终止了邓小平路线。 <br />
<br />
胡锦涛不仅这样说，也早在这样做了。在胡锦涛的主导下，去年的北京奥运会，被弄成一届不惜代价、不顾百姓死活、完全不顾经济效益的准军管奥运会；因为近乎癫狂和神经质的极权管制，北京奥运会的筹办和举办，不仅没有增加商机，反而带来了奥运期间经济“大萧条”――在“清场”及出入境“严管”等效应下，北京宾馆饭店空空荡荡、各旅游城市冷冷清清...胡锦涛一伙以不惜血本的疯狂管制，终于赢得了北京奥运会的政治“胜利”。 <br />
<br />
而牛年开年伊始，中央文件的精神要旨只有“稳定”，经济目标索性连提都不提了。而以往，胡锦涛多少得举着发展经济的牌子做做样子，早就想终止以经济建设为中心的，他先后炮制出“和谐社会”、和“科学发展观”口号，却又绝对禁止自由民主的话题，这就是为终止邓小平路线作“理论”准备。 <br />
<br />
当前病灶初起的中国经济危机，终于为胡主席公开终止邓小平路线提供了难逢的历史机遇：反正经济搞不去了，索性就坡下驴、踢开经济“闹革命”，高举“反西化”、“反分化”、“反和平演变”、“防颜色革命”、“反霸权主义”破旗，正好转移视线、迷惑人心。而这些时髦的名词，正是毛共“阶级斗争”的现行表述。 <br />
<br />
共产党统治下的所谓“阶级斗争”，就是对“阶级敌人”实行专政；所谓“专政”，就是对人进行无法无天、不择手段的迫害，用中共的话来说，就是“经济上截断、名誉上搞臭、肉体上消灭”；对“阶级敌人”的专政，胡锦涛中共越来越回归毛共时代，区别仅在于：毛共时期的“阶级敌人”主要是“地富反坏右”极其子女、党内走资派、胡共时期的“阶级敌人”则是民运、异议分子、维权人士、法轮功等独立宗教信仰组织；对政权异己分子，毛共称为“阶级敌人”，胡共则称作“敌对势力”。 <br />
<br />
毛共时代“阶级斗争”的迫害手法，胡锦涛早已不动声色地实行：对高智晟妻儿、陈光诚妻子、郭飞熊妻子、胡佳妻女、刘晓波妻子的迫害，实际上已经恢复了毛共时期亲属株连的专政手法；今年两会其间，全国各地的异议人士遭到文革以来空前的监控和打压，“两会”其间，“公民监证”行动的发起者郭永丰，甚至被不给任何理由地关入监狱...株连普遍盛行，好些异议人士恋人和配偶，都遭到威胁骚扰，要求划清界限... <br />
<br />
近年来，对“敌对分子”的打压，中共政治警察越来越采取扣押私人邮件冻结银行存款、抄家、抢走存折等赤裸裸流氓手段，不受任何法律约束。其手法越来越“文革”化，只不过文革时抄家中共用的是红卫兵，现在则是动用警察赤膊上阵。 <br />
<br />
胡锦涛时期的中共，对“敌对分子”的“斗争”之残酷，远远超过了邓江时期：2005年，胡锦涛一站稳脚跟，就制造“三班仆人”冤案，悍然冤杀徐先富等七名基督教领袖，其镇压宗教信仰的强硬和残暴，破了“改革开放”以来的纪录；胡共政权公然纵容黑社会势力殴打、杀害异己分子，在胡锦涛治下，民运、异议、维权人士在狱中普遍受到残酷虐待，踢打、电棒电击生殖器这样的酷刑竟用于对付高智晟、郭飞熊这样的名人，普通异己人士在狱中所受摧残更是可想而知。民运人士彭明、王荣清在狱中被整得患上尿毒绝症，王秉章、秦永敏等人也被折磨得疾病缠身。胡锦涛上台以来，对法轮功的迫害更是发展到了大规模活摘器官的地步。 <br />
<br />
今年伊始，在“步步高”的升级专制基础上，胡政权又祭出“整治网络低俗”运动，厉行新一轮网络大扫荡，中宣部严令“不留死角”，要把网上“自由化”等“错误”言论赶尽杀绝。紧接着又发起卷地毯式的出版业大清洗，《共和国记忆60年》或腰斩、《半夜鸡不叫》等一批有“政治错误”的已获准出版的书被禁止出版，甚至已经出版和发行的蔡楚诗集《别梦成灰》，都被强令在全国范围内收缴，而蔡楚九十年代还能够在国内自由出书，在这场剿杀出版自由的运动中，黑名单大大扩展，蔡楚、贺卫方、徐友渔等一批新人已经或有望被列入出版黑名单。中共国国家新闻出版总署还宣布：从4月1日起，实行“实名制”书号申领办法，出版社须以作者真名实姓申领书号，以堵死“敏感”人物用笔名出版书籍的渠道1。 <br />
<br />
胡锦涛发起的这场出版业的大清洗，其扼杀思想的专制力度，文革结束以来是空前的。除已经推行的手机实名制、已经拍板的书号实名制外，中国民众使用得最广泛的网络通信软件QQ，也正在酝酿实名制。中国网民的隐私和上网自由，也遭到“温水煮青蛙”式的扼杀。 <br />
<br />
综而言之，胡时代的专政措施，与毛共时期已经没有实质性区别，而形式区别仅在于只在于：毛共喜欢“挑动群众斗群众”、胡锦涛政权则动用警察赤裸上阵。事实上，现在除了还不敢大规模杀害政治犯外，胡共的专制统治，其精细和严密的程度比毛共时期有过之而无不及。 <br />
<br />
胡锦涛为什么要在这个时候重新高举阶级斗争的刀把子？因为邓小平（江泽民）路线已经走到头，再走下去就是颜色革命、和平演变。邓江路线造成专制管控不断收缩、以经济领域为基地社会自由空间不断扩展，为了公民社会的成长准备了条件，当今的中国公民社会已经萌动；在经济的大滑坡的形势下，底层怨愤倍增，而原先可以一心赚钱不问政治的资产阶级也无法照旧“闷声发财”，中共于社会精英的联盟趋于瓦解，整个社会日益涌起一股改旗易帜的共同愿望，中共法西斯权贵集团日趋陷入孤立，这种情形很像1988年的台湾和南朝鲜。面对这种形势，统治只有两条路可走：一条是象蒋经国那样主动放弃专制、一条是象苏联对付“布拉格之春”那样疯狂镇压，必要时不惜再次军管戒严、以“非比寻常的强硬手段”保住专制，但这样做的代价就是放弃经济发展、社会全面倒退。 <br />
<br />
胡锦涛显然已经选择了后一条路，其原因，除了胡某人所代表的当权派既得利益集团的邪恶本质外，中共政权本质不同于前台湾、南朝鲜政权也是一个原因：两蒋时期台湾政权、全斗焕南韩政权毕竟只是威权政权，公开承认普世价值；而中共政权的极权性质从来都没有改变，中共至今不承认普世价值，胡锦涛更是高调反对普世价值。 <br />
<br />
那么，中共胡政权下一步要往何处去？有人认为胡锦涛下一步要复辟毛共王朝，这并不确切。因为当今的中共，意识形态早已破灭，中共缺乏重回毛太祖时代的意识形态支撑；而且，尽管胡锦涛是毛泽东培养出来的辅导员，胡锦涛与毛泽东并不完全一样： <br />
<br />
从“文革”可以看出，毛泽东至少有一半是无政府主义者，这就注定了经由毛泽东之手创造的一切专制成果，也会在他的手中毁灭；文化大革命就是毛泽东领导的中共自残运动，如果毛泽东有邓小平的寿命，中共政权一定会随着毛的死亡而彻底崩溃。 <br />
<br />
而胡锦涛则是百分之百的“布尔什维克”（专制主义者），他虽然高举毛泽东，却坚决反对“文革”，而“文革”才是毛泽东的灵魂。毛泽东要砸烂所有的坛坛罐罐，而胡锦涛则要拼命箍紧一个桶（尽管这只老朽的桶箍得太紧反易破裂）；胡锦涛更象陈云、斯大林、金日成父子。失去了共产主义意识形态支撑的中共，要重新扩张极权统治，唯有借助民族主义和中国传统文化糟粕。胡锦涛正在切切实实借助这两件“法宝”： <br />
<br />
胡锦涛上台不久，就刻意大力推崇儒家糟粕，以为专制正名、接连视察军队、发表讲话，要求野战军随时作好对内镇压的准备、最近，胡锦涛以“中华武德”高调反对军队国家化，这一歪曲利用中国传统的以保专制的新招数，确是毛建国以来的创举。 <br />
<br />
与胡锦涛紧密配合的是：温家宝最近高调谴责西方“不负责任”的经济制度，把中国的经济危机完全归咎于美国的经济制度，以此大力煽动国内经济民族主义狂热，以掩盖自己的决策失误，顺便挑起反美、反西方虚火，转移自由民主视线。而导致经济危机的“西方阴谋”说，刚好可以用来点燃反美民族主义虚火，为“抓纲治国“服务，以便顺理成章把所有“自由化“分子打成汉奸洋奴、美国走狗… <br />
<br />
这些迹象表明：胡锦涛一伙正挟持诱拐中国走向的道路，是一条有中国传统糟粕特色的极权法西斯道路，它在经济上类似于纳粹德国，政治上兼有纳粹德国、李氏新加坡、金家朝鲜三种特征。与纳粹枪口对外不同，胡锦涛正在紧锣密鼓构建的新极权的中共国，是一个枪口对内的纳粹德国，它把弱势群体当作“劣等民族”来压榨，它要把“敌对分子”当作犹太人来处理。 <br />
<br />
综上所述，胡锦涛已经重新“以阶级斗争为纲”。而不管是毛时代毛派当权者＋流氓无产者对其他阶级的“阶级斗争”，还是现在的中共法西斯流氓官僚资产阶级对其他阶级的“阶级斗争”，都是祸国殃民的人权大灾难。若不能尽快瓦解中共专制，中国人自毛共暴政之后，“吃二e苦，受二e罪”的危险真的会降临每个人头上。</div>

							
							
							
							
