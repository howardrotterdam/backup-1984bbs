																											<div id="postmessage_771558" class="t_msgfont">最近频繁发生政治震荡的吉尔吉斯斯坦，典型地表现出了其特有的中亚社会不稳定症结：北方的吉尔吉斯人与南方的乌兹别克人的矛盾冲突、政权的合法性和代表性不足。而吉尔吉斯斯坦的问题几乎存在于每一个中亚国家。<br />
<br />
某种程度上看，民族问题加上不民主的政治制度，中亚多少存在巴尔干化的隐忧。<br />
<br />
中亚再往西去，便是俄罗斯视为核心利益之所在的高加索地区。高加索地区实际上已经是另外一个巴尔干火药桶，它之所以未发生爆炸性冲突震荡，是因为俄罗斯在此尚拥有压倒性的力量，同时，也没有第二个强国敢在此与俄罗斯公开对抗。但是，如果伊朗政权在外界渗透和制裁下发生突变，则库尔德斯坦、格鲁吉亚问题可能会一起爆发，高加索地区目前的稳定很可能会被打破，震荡不但将长期持续而且很可能会波及中亚地区。<br />
<br />
中国的问题在边疆，边疆的问题在新疆，新疆不稳定，则中国在中亚的国家利益保障无从谈起，新疆既然不应被视为边疆，而是以腹心之地视之，则应放宽视角，广泛学习那些成功缓和了民族矛盾乃至分离主义倾向国家的经验，本着为千秋万代子孙着眼的历史高度，以大智慧解决民族矛盾。<br />
<br />
<br />
<br />
<br />
<br />
除了金钱的力量，我们还有什么力量<br />
<br />
随着中国经济实力持续而强劲的增长，中国对事关国家大政方略的财政投入能力空前提高，甚至让西方老牌发达国家瞠目。<br />
<br />
但是，钱多了只意味着国家硬实力的增长，不意味着软实力有了相应提升，因为很多难题并不是只靠堆钱就可以解决。今天的中国社会，令人担忧的一个现象是，从上到下都洋溢着有钱好办事、钱能摆平一切的热情。它带来的是急功近利、用钱买路的逻辑和行为方式，完全忽视必不可少的长期细致工作，以及自我形象的提升。用钱开路，只有油水情而绝无鱼水情，换来的往往是顺水推舟、要价一路水涨船高。<br />
<br />
以中国在非洲的投资为例，中国商人在国内直接用钱向官员买路的行为方式，在这里被发挥到了淋漓尽致的程度。但是，非洲的政府对社会的治理能力和控制能力远无法与国内相比，钱只是摆平了官员却摆不平当地老百姓，当地部族游击队隔三岔五扔个炸弹或发一封恐吓信的事情经常发生。而且，金钱开道的方式不但使这些官员的胃口越来越大，也让当地百姓对中国政府、中国企业的形象极为反感。<br />
<br />
而西方国家进入非洲一类国家时，通常既有耐心打通官员的一面，又有提出苛刻条件约束其行为的未雨绸缪，同时不忘对当地社会进行有效渗透和攻心工作。经济的一条腿伸进去时，文化和政治辐射的一条腿也伸了进去。单以金钱开道，雨来地皮湿，雨过一风吹。作为一个第三世界国家，中国与第三世界国家打交道的经验，往往反不如西方。<br />
<br />
谈到中亚，我们的视线首先会落在俄罗斯和美国这样的竞争对手上，但拉开了距离看，恐怕最有力量的竞争对手，是土耳其而非美国和俄罗斯。<br />
<br />
俄罗斯有军事力量没有经济力量，更缺乏道德和文化的号召力；美国什么都不缺，但中亚只是它推行“自由民主圣战”和大国角逐战略的疆场，它无意也无心经营中亚；而土耳其则不然，虽然它既无足够军事力量也无足够经济力量，但它却在突厥民族中拥有无与伦比的价值观和文化的感召力。<br />
<br />
在政治和意识形态上，土耳其是伊斯兰世界和突厥世界中，世俗化和民主化的最好榜样；在文化上，土耳其自称是一切突厥民族的祖国，它虽不算富有，却在中亚地区大量进行文化投入，援建大学、图书馆和其他文化教育事业，输出土耳其的拼音文字系统，传播土耳其的世俗文化，对所有突厥语民族的留学生留有大量政府补贴的名额。<br />
<br />
不到20年时间，从亚德里亚海到中国的玉门关的“突厥走廊”里，到处都流行着土耳其音乐、土耳其影视，青年学生出国留学，要么选择西方发达国家，要么选择土耳其。<br />
<br />
没错，中亚五国市场上到处可以见到中国的小商品，汉族商人卖中高端产品，维吾尔商人卖中低端商品，但换来的却是中国把中亚变成商品倾销市场和原料产地的疑惧。虽然土耳其在中亚与中国的竞争中，经济上、尤其是资源争夺中落了下风，但却在人心的争夺中，占据了上风。<br />
<br />
单靠金钱的力量，不但无法长期保有中国在境外的国家利益，甚至亦不能保有境内的平安稳定。<br />
<br />
迷信金钱力量的民族，是落后和愚昧的民族。无论是用于内政的安抚还是世界的拓展。拥有经济和文化及意识形态双重优势的民族，才是真正强盛的民族，才是值得拜服的、有感召力的民族。<br />
<br />
民族竞争是全方位的竞争，是综合素质的竞争。决定民族命运的绝不仅仅是军事和经济力量，而主要取决于文明形式本身。民族的生存决定我们必须进行政治体制改革。人类社会，以十年为度，可见政治兴衰；以百年为度，可见社会兴衰；以千年为度，可以看到文明的兴衰。<br />
<br />
<br />
<br />
<br />
<br />
十年之内，一场由威权政治向民主政治的转型，不可避免地要发生。中国将会出现伟大的变局。政治体制改革是历史赋予我们的使命。我们不可能有退路。<br />
<br />
<br />
<br />
<br />
<br />
<br />
中国的改革走到今天，容易改的早已改尽，剩下的都是最困难的部分，每一步都要闯地雷阵。苏联的改革则是由难到易。他们已经走过了最难的一关。而中国最难的一关尚未过呢。苏联的教训任何时候都是中国的镜子。苏联的失败主要在于内因。它并没有被世界大战打垮，却在制度竞争中被打垮了。一个制度如果不能让公民自由地呼吸并最大程度地释放公民的创造力，不能把最能代表这个制度和最能代表人民的人放在领导岗位上，它就必然灭亡。<br />
<br />
当年苏联所有的问题都是体制上的问题。上层不能照旧生活下去，下层不能照旧统治下去。苏联也曾强调稳定，把稳定看成了目的，把维持现状看作实现稳定的手段，稳定压倒一切，金钱摆平一切，结果却激化了矛盾，一切反过来都可以压倒稳定。<br />
<br />
一个有着远大理想和抱负的民族，绝对不应迷信一时的金钱力量。<br />
<br />
它可以信赖的，首先是观念的力量。中国传统政治奠基于商鞅，粗成于秦始皇，大成于汉武帝。独尊儒术就是独尊皇权。正因为这种背景，使得百多年来中国多次借鉴西方的政治改革都半途而废。改革在其他领域都能大显神通，一旦延伸到政治领域，马上就寸步难行，最后总会爆发突然事件，使改革者措手不及，全军溃败。邓小平是要进行政治体制改革的，也曾下定决心，但随着形势的发展，政治改革与传统政治权力结构的不相容性日益凸现出来，导致了波及全国的动乱。<br />
<br />
它可以信赖的，是制度的力量。美国成功的秘密不在于华尔街，也不在于硅谷，真正的秘密在于长盛不衰的法治和法治背后的制度。美国制度被诩为是“一种由天才们设计，使蠢才们可以运作的体系”。一种坏的制度能够使好人做坏事；一种好的制度也能够使坏人做好事。民主恐怕是极要紧的。没有民主，就没有持久的崛起。民主思想的传播不受国界限制，当然也不受历史限制。中国的精英们要有为后代寻找一条既符合中国国情又切实可行的制度的勇气。不一定要成为先烈，但一定要成为先驱。<br />
<br />
它可以信赖的，是真理的力量。真理即知识，知识即力量。中国不缺乏真理，中国缺乏容忍真理存在的土壤。在西方，资产阶级大革命时，思想的风暴引发了革命的风暴。在中国，在文化大革命中，革命的风暴毁灭了思想的火种。我们缺少现代极为珍贵的反思意识。思想投降了，真理无处容身。今天我们可以表达自己的意见，但主导世界的不是意见，而是思想。意见不是力量，思想才是力量。没有思想，谈什么“解放思想”？文化大革命最显着的特点是：不许讨论。举凡攸关民族命运的问题，都不允许公开讨论。允许公开讨论的都是小问题。其实小问题无需讨论就可以处理，大事才需要公开讨论。邓小平当年发动的“真理标准大讨论”，是那样激荡着中国人的心。这场讨论还在民众心中种下了独立思考的种子。国力最重要的一部分是人民的思考力和论述力。</div>

							
							
							
							
