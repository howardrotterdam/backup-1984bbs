&nbsp;&nbsp;女老师忙摆手说：“没关系，这个是自然状态，我能接受。”<br />
&nbsp;&nbsp;我情绪有点失控，把国保给我的笔和纸一推，说：“我不写了，大不了你们把我拉到提篮桥去。”<br />
&nbsp;&nbsp;胖国保说“你还是写吧，你不写，我们不好交差。”<br />
&nbsp;&nbsp;我想国保始终还是国保，我不写，他们也不会放我走，还是老老实实写吧。但我的情绪还是很激动。<br />
&nbsp;&nbsp;我说，《奥运金牌和灾后重建》，金牌的“牌”我不会写，我写文章都是打字的，手写一下子想不起来怎么写了。<br />
&nbsp; &nbsp;瘦国保说：“金牌的牌写不来，就用P代替，就写个金P。”<br />
&nbsp;&nbsp;胖国保说：“这有什么写不来，旁边一个片。”<br />
&nbsp;&nbsp;听到这里，我想起来牌字怎么写了，就写在纸上了。<br />
&nbsp;&nbsp;瘦国保说：“我问你，你在纵横中国上发表过文章吗？”<br />
&nbsp;&nbsp;我说：“什么纵横中国，纵览中国，这个月刚发了两篇。”<br />
&nbsp;&nbsp;瘦国保说：“你都写下来，文章标题，笔名，发表媒体，都写下来，我们要回去交差。”<br />
&nbsp;&nbsp;这时我也想快点回家看球。就想快点了结这件事。就开始写。没想到又碰到一个字写不出来。<br />
&nbsp;&nbsp;我没好气地说：“《请中国政府善待朝鲜难民》，朝鲜的鲜写不来。”<br />
&nbsp;&nbsp;瘦国保说：“这你都写不来？还大学生啊？鱼羊鲜吗。”<br />
&nbsp;&nbsp;我又折腾了好长时间，终于写好了，拿给胖国保看。<br />
&nbsp;&nbsp;胖国保说，下面签个名。我想，反正横竖横了，就签了个名。<br />
&nbsp;&nbsp;瘦国保说：“名字下面把日期写上。”<br />
&nbsp;&nbsp;我问：“今天几号？”<br />
&nbsp;&nbsp;瘦国保说：“5月22号，09年5月22号。”<br />
&nbsp;&nbsp;终于大功告成了，我也想回家了。<br />
&nbsp;&nbsp;一看手机，11点40了。对胖瘦二国保说：“我要回家写稿赚钱了。”<br />
&nbsp;&nbsp;瘦国保说：“你还是先把稿费拿到再说吧。”<br />
&nbsp;&nbsp;胖国保说：“你下午有课吗？我们再聊聊。”<br />
&nbsp;&nbsp;我说：“没课，和你们没什么好聊的，我要吃饭了，11点40了，你看。”<br />
&nbsp;&nbsp;胖国保说，吃饭不急，我们请你吃饭。<br />
&nbsp;&nbsp;听到这句话，我很生气，说：“什么你们请我吃饭，还不是花纳税人的钱，你们的工资哪来的？政府给的。政府的钱哪来的，还不是从老百姓哪里来的。（边说边拿起放在桌上的mp3）我买这个mp3就交了消费税，我是纳税人知道吗？”<br />
&nbsp;&nbsp;胖国保说：“知道，知道，你是纳税人。我们是为你服务的。”<br />
&nbsp;&nbsp;我说：“你们的工资还不是老百姓那里来的。现在你们用这些钱来对付我，真是取之于民，用之于民啊。”<br />
&nbsp;&nbsp;我继续说：“我不就是给海外媒体投稿了吗？我也想给国内媒体投稿啊，但我也不知道怎么写能通过审查。又没人教我怎么写那种可以通过审查的批评类文章。有的人写的文章，含沙射影，一看就是对共产党很不满，但是又揪不住他的辫子，我写文章就是单刀直入，实话实说。比如批判高房价，有人骂房地产商，但是关键还是地方政府，骂房地产商的文章可以通过审查，骂地方政府的文章就通不过审查。我可管不了那么多，房地产商和地方政府一起骂。像民营企业家是软柿子，很多人都在骂。但是他们和高干子弟比，钱少得多，高干子弟比民营企业家坏多了。但骂高干子弟风险太大，许多人就不敢骂。我也不管，就算高干子弟我也照骂不误。我也想写那种可以通过审查的文章，那种文章稿费更多，但我实在写不来那种含沙射影含而不露的文章。”<br />
&nbsp; &nbsp;胖国保说：“那是你水平不够。”<br />
&nbsp; &nbsp;胖国保说：“你给我个手机号，我们以后再找你聊。你说的一些问题在现实中都存在，我们就是了解了解你的思想动态。”<br />
&nbsp; &nbsp;我把我的手机号报给了胖国保听，说：“还是不要聊了，帮你们聊又没有钱。”<br />
&nbsp; &nbsp;瘦国保说：“不如你写稿赚钱是吗？你还是快去把稿费要回来吧，他们说不定要赖帐哦。”<br />
&nbsp; &nbsp;于是我和国保的谈话就这样结束了。我坐电梯离开了综合楼，去学苑食堂吃午饭。感到今天的遭遇很荒诞，本来以为是学校老师找我谈话，没想到碰到了国保。国保找我谈话也不提前通知我，还冒充什么学校领导找我谈话，不给我心理准备，看来就是要套我的话，如果国保的目的要套我的话，那他们确实成功了，我在一个半小时内说了那么多，但我也无所谓，反正人迟早是要进骨灰盒的，活着的时候不要考虑太多。<br />
 <br />
全文完<br />
