<br />
&nbsp; &nbsp; 2009年5月21日,我接到老师电话，要我第二天上午10点赶到同济大学综合楼1101室，叫我无论如何一定要去，就算有课也要去。一开始我以为是因为我缺课太多，学校老师要找我谈话。没想到居然是国保找我谈话。从上午10点一直谈到了11点40分。因为这次谈话笑点很多，我就把他记录下来，也算纪录一段历史。<br />
&nbsp; &nbsp;10点还不到，我就赶到了同济大学综合楼1101室。原来这里是同济大学人文学院的办公室。一个我不认识的老师接待了我，她说什么校领导要找我了解点情况，叫我先坐下来等等。过了几分钟，来了一胖一瘦两个男的，戴着眼镜，样子很斯文，乍一看还以为他们是学校的老师，后来才知道是国保。他们好像不想在人文学院的办公室和我谈，因为旁边有许多老师在办公，隔墙有耳，国保办事一向低调。同济某女老师就给他们找了综合楼11楼的一间空教室，让他们在里面和我聊。<br />
&nbsp;&nbsp;然后我，同济某女老师，胖瘦二男国保，就进行了下面的对话。<br />
&nbsp;&nbsp;我解下挂在身上的mp3，放在课桌上。瘦国保拿起这个mp3玩，问我这个mp3容量多大，我说1G，瘦国保又问我平时听什么歌，我说，听周杰伦的歌。<br />
&nbsp; &nbsp;胖国保单刀直入，说：“我们是政府的人，来找你了解点情况，你知道是为什么吗？”<br />
&nbsp; &nbsp;听到这句话，我才知道他们是国保。既然他单刀直入，我也没什么好隐瞒的。<br />
&nbsp; &nbsp;“因为我在网上发表了点文章。”我回答道。<br />
&nbsp; &nbsp;胖国保问：“什么类型的文章？”<br />
&nbsp; &nbsp;我回答：“就是批评政府的，批评共产党的。”<br />
&nbsp; &nbsp;胖国保说：“你还记得是那几篇文章吗？发表在什么网站？”<br />
&nbsp; &nbsp;我回答：“当然记得，自由圣火9篇，议报3篇，纵览中国2篇。”<br />
&nbsp; &nbsp;瘦国保插话道：“你还记得挺牢的吗，那些文章你电脑里还留着吗？”<br />
&nbsp; &nbsp;我回答：“当然保存着。我写的每篇文章电脑里都存了。”<br />
&nbsp; &nbsp;瘦国保笑着说：“你还要留着自己欣赏啊。”<br />
&nbsp; &nbsp;我也笑着回答：“对，我以后还要搞个文集呢。”<br />
&nbsp; &nbsp;胖国保问：“你为什么写这些文章呢？动机是什么？”<br />
&nbsp; &nbsp;我说：“为了钱，为了稿费。”<br />
&nbsp;&nbsp;听到这句回答，胖瘦国保有点惊讶。<br />
&nbsp; &nbsp;瘦国保问：“你写一篇文章有多少稿费？”<br />
&nbsp; &nbsp;我回答：“50澳元到80美元吧。很多钱呢，以前澳元汇率高的时候1比5.8，一篇文章就有快三百多块钱呢，后来去年一段时间人民币升值，澳元大贬值，澳元比人民币的汇率从5.8跌到4.4了，我就心灰意冷懒得写了，后来汇率又升回去了，到5.2了，我一看又有钱赚了，就又开始写了。”<br />
&nbsp; &nbsp;胖国保问：“你第一篇文章的题目是什么？”<br />
&nbsp; &nbsp;我回答道：“《奥运金牌和灾后重建》”<br />
&nbsp; &nbsp;胖国保问：“什么时候发表的？”<br />
&nbsp; &nbsp;我回答：“去年8月。”<br />
&nbsp; &nbsp;瘦国保插话道，“哦，就是奥运会那段时间吗。”<br />
&nbsp; &nbsp;瘦国保接着问：“你说你是为了钱写的，那你稿费拿到了吗？”<br />
&nbsp; &nbsp;“还没拿到。”我回答道。<br />
&nbsp; &nbsp;瘦国保笑了笑，你说你钱都没拿到，那你忙活了大半年图个啥。你怎么知道稿费一定拿得到，你怎么知道他们不会赖账呢。<br />
&nbsp; &nbsp;我说：“现在没拿到，以后总会拿到的，他总不至于赖我这点稿费吧。再说了，如果我文章写得好出名的话，以后给的稿费会越来越多的，说不定还能当个作家。港台的中文媒体说不定会请我写专栏，什么明报啊，苹果日报啊，当然凤凰卫视不会请我。（笑）”<br />
&nbsp; &nbsp;胖国保说：“凤凰卫视当然不会请你，我再给你补充一个，东方日报。”<br />
&nbsp; &nbsp;接着胖国保开始严肃起来了，说：“你一个名牌大学的学生，赚钱的方式有很多种，怎么会想到这种方式？你知道你对父母，对学校，对社会的责任吗。”<br />
&nbsp; &nbsp;我也一本正经地对胖国保说：“你不要帮我扯这些大道理，我这人没什么信仰，什么道德体系都没有，价值体系都被摧毁了。什么仁义礼智信，在我眼里都是狗屁。我只认钱。什么尊师重道，孝敬父母，在我眼里都狗屁不是。什么对社会的责任，对父母的责任，都是屁。就算明天同济大学把我开除了，我也不在乎。”<br />
&nbsp; &nbsp;胖国保说：“你考进大学也不容易，怎么能这么想呢。我弟弟就是同济大学的。你考进去也不容易，要珍惜啊。赚钱的方式有很多种，你怎么会选择这种呢，你是怎么想到的？”<br />
&nbsp; &nbsp;瘦国保插话道：“你是怎么接触到自由圣火这个网站的？在破网软件上看到的？是谁给你破网软件的？”<br />
&nbsp; &nbsp;我回答到：“破网软件是我在网上找的。”<br />
&nbsp; &nbsp;瘦国保问：“在什么网站找的？给你破网软件的人你认识吗？”<br />
&nbsp; &nbsp;我说：“校内网，不认识。”<br />
&nbsp; &nbsp;瘦国保说：“人你都不认识，你怎么会拿的？你要破网软件干嘛？是不是为了上黄色网站？（国保真是料事如神）”<br />
&nbsp; &nbsp;听到这句话，我连连点头，说：“对对，一开始我就是为了上黄色网站。后来才想到写文章赚稿费。”<br />
&nbsp; &nbsp;我接着说：“我打开破网软件的主页，上面有自由圣火的网址，点进去一看，说写文章有稿费，我又看了看它上面的文章，感到水平不见得很高。我也可以写写，稿费一篇有50澳元，如果我一个上午能写一篇文章的话，就赚翻了，你知道我做家教2个小时才50人民币，还要赔上中介费，路费，路上的时间，加起来一个上午还不到50人民币，只要有灵感，写稿一个上午就有50澳元，50人民币和50澳元啊？差距多大啊。然后我就开始写稿，没想到第二篇投稿就发表了，我发现自己很有写作天赋，就开始投稿了。”<br />
&nbsp; &nbsp;我又笑着对胖国保说：“我回去把我们的对话写下来，说不定又可以赚稿费了。”<br />
&nbsp; &nbsp;胖国保也笑着回答：“你回去写好了，我们无所谓。接着又板起脸问我，你知道你这样做的后果吗？”<br />
