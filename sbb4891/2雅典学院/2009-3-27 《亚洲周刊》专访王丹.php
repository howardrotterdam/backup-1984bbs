记者： 海涛 <br />
香港报<br />
Mar 26, 2009<br />
&nbsp;&nbsp;<br />
<br />
今年是89年六三六五事件20周年。香港支持大陆民运团体港支联计划邀请当年的学生领袖王丹来香港，参加纪念活动。不过，王丹从98年获释到美国后，还没有得到进入香港的机会。<br />
<br />
今年6月是天安门事件20周年。连续20年举行纪念活动的香港市民支援爱国民主运动联合会(港支联、支联会)，今年计划邀请哈佛毕业的王丹博士来香港参加悼念活动。<br />
<br />
八九民运期间，王丹是北京大学学生，曾担任保卫天安门广场副总指挥。镇压之后，政府通辑21名学生，王丹名列第一。90年被捕，91年以“反革命宣传煽动罪”判刑4年。93年假释，95年再度被捕。96年，以“阴谋颠覆政府罪”判刑11年。98年保外就医，来到美国。入读哈佛大学。01年，得到硕士学位，08年，得到博士学位。当时，王丹表示愿意来香港教书，但没有成功。<br />
<br />
*李卓人：考验香港一国两制*<br />
<br />
 <br />
李卓人 <br />
支联会副主席李卓人议员说，星期三的立法会上，他向政府入境处长白韫六质询，港府是否有个不准入境的黑名单，得到的回答是否定的：“我们支联会希望，王丹能在六三六五20周年时来到香港，参加我们的六三六五烛光晚会。当然我们最担心的就是他可否入境香港。昨天我在立法会，问这个问题时，入境处长否认香港有黑名单。所以我们今年想试验一下，看王丹能不能来香港参加。”<br />
<br />
去年8月，王丹曾得到邀请，来港参加一个非政治性的座谈会。但中国驻洛杉矶领事馆拒绝为其办理签证。<br />
<br />
支联会另外一名副主席蔡耀昌说，王丹一直希望能来香港，既然香港实行一国两制，所以，支联会计划邀请王丹来香港参加纪念活动：“我们知道，王丹一直都很希望来香港。所以，我们今年特别要发出邀请，希望可以邀请王丹在六三六五期间，一起在香港做纪念活动。”<br />
<br />
 <br />
蔡耀昌 <br />
有舆论说，王丹入境问题，这不是香港政府所能决定的。入境处长的讲话，不说明任何问题。针对这个观点，李卓人说，理论上来说，香港实行一国两制：“理论上，香港实行一国两制，香港政府应可以自行决定。我们觉得，按照法律按照宪法，王丹应该可以进来。当然，现在大家都知道，现在的一国两制，很多方面已经没有了。所以，我们这次采取行动，就是要看看政府方面是否还会实行一国两制，让香港政府来具体执行。”<br />
<br />
支联会这次除了邀请王丹，还邀请丹麦艺术家高志活来香港，修复“国殇之柱”上的裂开的洞口。高志活塑造的目前竖立在香港大学的“国殇之柱”，已经屹立在室外风雨中12年了。高志活去年在奥运前夕来到香港，也被香港海关移民当局拒绝入境。<br />
