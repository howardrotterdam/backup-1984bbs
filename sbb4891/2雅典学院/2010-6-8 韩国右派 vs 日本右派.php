													<h2>韩国右派  vs  日本右派</h2>
						
						
																											<div id="postmessage_665097" class="t_msgfont">[特派记者评论]韩日保守阵营有何不同？<br />
<br />
朝鲜日报驻东京特派记者 鲜于钲 (2010.06.08 13:51)<br />
6月4日，在选举日本下任首相的民主党党首选举中，一位名叫樽床伸二（50岁）的陌生人物参加了竞选。他虽然五次当选众议员，但知名度并不高，因此，他输给了民主党第一代党首、现任财务大臣菅直人。但是，无论是否当选，他的参选都值得关注。因为，他的经历象征性地体现出引领未来日本政治的下一代主角的面貌。<br />
<br />
他是保守派，重视美日同盟，将机会平等和良性竞争视为政治哲学。他曾指责前首相鸠山由纪夫重新调整冲绳美军基地迁移计划的意图，并主张同意识形态不同的社民党分离，解散联合政府。他的这种政治哲学反而和自民党更相似。但是，他没有选择自民党。因为，让子女世袭议员职务的腐朽的自民党不会把位置让给贫穷的樽床伸二。<br />
<br />
他出生于贫穷的地方，是家里的长子，父母是加工西装的普通工人。他小时候亲眼目睹自己的妹妹出车祸死亡。他的爷爷是个盲人。他当天发表演讲时表示：“我的爷爷告诉我，无论面对什么情况，都要靠自己的双腿站起来并坚强地活下去。”他说，“机会平等”和“良性竞争”这种保守哲学也是双目失明的爷爷教给自己的。因为受伤，他不得不放弃成为棒球运动员的梦想。而将他带入政治世界的是一家名为“松下政经塾”的政治学院。这是大型家电企业松下创始人为那些因为没有金钱和背景而无法实现政治梦想的贫困人才开设的学院。该学院具有保守倾向，樽床伸二是这里的第三届毕业生。<br />
<br />
民主党内有30名国会议员毕业于政经塾，他们大都属于保守派，比自民党（6名）要多。最具代表性的是前原诚司议员（48岁），他的父亲在他小的时候自杀身亡，留下他们母子相依为命。在党内领导政经塾出身势力的野田佳彦议员（53岁）是自卫队队员的长子，曾依靠燃气检查员的工作维持生计。<br />
<br />
他们比韩国曾被称为“386一代”（60年代出生，80年代上大学，30多岁的群体）的韩国40岁年龄段的人略微年长一些。但是，他们上大学时非常平静，意识形态斗争已基本铲除。最重要的是，他们得到了国家给予的很多实惠。当时日本的经济状况从高度增长时期过渡到了稳定增长时期。国家财富通过政府的再分配而流入低收入层和地方手中。统计结果显示，20世纪70年代至90年代，被分配到日本地方的政府财政达城市财富的1.5倍以上。<br />
<br />
得益于此，樽床伸二虽然贫穷，但并不悲惨。前原诚司从高中开始拿奖学金，最后毕业于名牌大学京都大学法学系。也就是说，他们这一代经历了“虽然贫穷但却可以获得成功”的社会氛围。而当时推进这些政策的政党就是自民党。他们推进这些政策也曾受到“带着资本主义面具的共产主义”、“大众迎合主义”等指责。但如果没有这些政策，当今日本就会落入怨恨社会、憎恶保守的政客手中。<br />
<br />
韩国的保守派自己把如此重要的保守价值拱手让给了对方阵营。要想使保守价值得到贫穷人才的尊重，就应该让他们体验到保守国家并不会无情。日本做到了这一点，所以贫穷的人不憎恶保守。因为国家和社会培养了贫穷的保守、健康的保守，所以日本的意识形态基础不会轻易发生动摇。<br />
