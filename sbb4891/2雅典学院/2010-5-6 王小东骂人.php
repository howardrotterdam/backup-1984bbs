<br />
今天跟玉昆连线做了个节目，关于美国警察冲入中国驻休斯敦总领事馆殴打郁伯仁副总领事的事件。我这次确实是大骂了人。这是直播节目，但也有三秒钟的延迟，不知掐了多少。我估计以后也不会有机会再上这个节目了，给玉昆带来麻烦也对不起了。<br />
<br />
 <br />
<br />
这个事情本来很清楚：是美国警察违了法，他们无权进入中国领事馆，也无权拘捕具有外交豁免权的郁伯仁。一个“不知道”不能免除其违法的事实：我要是在中国街头杀死一个美国人，然后说我“不知道”这样做会杀死他，我就可以脱罪吗？但在消息传到中国后居然有这么多的网上贱民为美国人叫好，这实在太叫人恶心了。这些贱民叫好的理由是，郁伯仁是个贪官，在中国作威作福惯了，想到美国也充大爷，结果叫美国人揍了。我不知道认定郁伯仁是贪官的根据是什么，但退一万步讲，就算郁伯仁是贪官，他也是我们的贪官，许我们揍，也不能允许美国人非法欺负他。美国人有这样的意识，英国人有这样的意识，日本人也有这样的意识：就为了一毒贩他们还跟我们没完没了呢！你作为一个人，连这点意识都没有，你还算人吗？你就是个屁民，叫你屁民都是抬举你，你就是个贱民！你活该被中国的贪官欺负！反正你不被中国的贪官欺负，你也会被世界上无论什么国家的外国人蹂躏。被中国城管打了你晖勐医校我看要是被美国人打了你就一定会很爽。反正你是一个挨打也能感觉爽的人，被中国城管打时，你就想象一下那是被美国城管打，你不就爽了吗！你不就不痛苦了吗！<br />
