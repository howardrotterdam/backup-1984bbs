													<h2>往事的确并不如烟――评孔庆东</h2>
						
						
																											<div id="postmessage_861501" class="t_msgfont">作者：【盖斯了 】 发帖时间：2004-11-07 10:56:28&nbsp; &nbsp;&nbsp;&nbsp;<br />
<br />
发言内容： <br />
北大的孔庆东博士的言论，的确让我非常非常难以理解，难以接受。套用一句流行的话来说，他的言论已经超出了底线――而且不仅仅是学术的底线，是做人的底线，这个问题和信仰没有关系，无论你信仰什么主义，你首先都是一个人。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 我并没有经历过饥饿的年代，我很庆幸没有这样的体验。我知道那时候生活非常艰难，全国性的艰难，每个中国人都有权说“生活艰难”这句话，国家的外债――国家的外债需要国民用生命去偿还吗？国家的基本职能就是保护国民，无论是什么主义的国家，如果一个国家以“人民”的名义屠杀人民，我为什么要认同他？比如说吧：抗日战争我们牺牲了三千万人赢得了战争，我觉得这个牺牲就不存在值不值的问题，是必须的，必须付出。但我不认为国家的独立是唯一的理由，我不会把这个问题抽象出来，我要具体地讨论。人的生命，这的确是没有价值的，国家的独立是为了保护国民，所以，抗战的牺牲其意义在于国民生命得以更好地保全。必须基于这个理论基点：如果不抗战，我们必定会付出远远超过三千万生命的代价，所以三千万生命的丧失尽管是惨痛的，但却是必须的，这一点上的任何调和论都是错误的。但是，假如一种“人祸”――比如以国家名义借的外债，造成了更多的人生命的丧失，我们该如何评介这种国家行为？该如何原谅？更何况外债说仅仅是借口之一（这个问题我们可以从历史的轨迹寻找答案，当一个国家不能保障自己国民的生命时，被替换掉是天经地义的，国家的独立不能脱离于人的具体生命的存在）。孔庆东轻轻一句“国家的外债”就将这件事情一笔带过，其无非表达两层意思：其一，全国人民挨饿（仅仅是挨饿吗？是被饿死）是因为国家借了外债，所以对“国家行为”你只能接受，至多你表示沉默（事实上接近三十年，你无沉默的权利），剥夺你发牢骚的权利；其二，我比你还苦，你算什么。后一个观点有一定的理由，基于人的自然属性所延伸的小市民心理，却一定和“学者”没有关系。至于“共产党对他们的政策是极其宽大的”之类的梦臆就不属于讨论的范围了。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 反右的问题。实际上这是一个清除同盟者的事件。因为按照党的统一战线政策，无产阶级的同盟者主要有三种（当时）：农民、城市小资产阶级、民族资产阶级。而知识分子（右派）其实绝大多数被定为“城市小资产阶级”的――这个阶级曾经被当作“中国革命的基本动力之一”，是无产阶级坚定的同盟者，统一战线其实主要是统一小资产阶级和民族资产阶级。但遗憾的是，这种统一却在建国后不到八年的时间被完全毁坏。其一：利用赎买政策事实上消灭了民族资产阶级。其阶级分子不是被“改造”，就是被“专政”，基于合法财产的丧失，相当于失去了保护自己的能力（甚至失去了可以利用行贿手段来保护自己的能力），这种能力的丧失为以后文革的暴捩埋下了伏笔。其二，对城市小资产阶级的清算，这种清算体现于反右，其本质上是对思想界异端因素的清除。这里要绝对提出的一点是，城市小资产阶级（知识分子）这个阶层，他们对革命的支持是基于国民党极度腐败和独裁的现状而作出的选择，这种选择本身是有条件的，例如民主的真正实现，政治协商会议的召开等等。一旦这些条件丧失，他们必然会有激烈的反应――这本是这个阶层的本质属性。这种反应本身是经不起上纲上线的（任何反映哪怕是生理反应都经不起上纲上线），而这一属性却被利用来进行思想净化工作。这个事件的前因后果想必不需要具备“博士”水平才能清楚吧？我想说的是，孔庆东说：“你应该站起来说：我就是反对共产党，我是堂堂正正的英雄”，“这人格放在资产阶级里也是卑鄙的啊”。我被这两句话震撼了。<br />
&nbsp; &nbsp;&nbsp; &nbsp;<br />
&nbsp; &nbsp;&nbsp; &nbsp;我想起二战，资产阶级的欧洲，面对希魔的屠杀，有多少人站出来说“我就是反对你，我是堂堂正正的英雄”呢？说“我就是反对你”固然是一种美德，但这种美德却需要建立在一定的机制之上，没有任何人有权利要求大家都成为文天祥或圣女贞德，孔博士你也没有这种的权利，而且我断言你也没有这样的美德。我们需要这种美德，我也希望全国的知识分子都能仗义执言，但我们却没有这种机制保障。我反对你，需要一个基本前提：我不会因为反对你付出生命的代价。（这里所说的反对并非暴力性质的活动，而是思想上的一种对抗），资产阶级之所以有这种美德，是因为他们不会因为反对某一个党就失去生命，所以他们有这样的美德。美德是制度造成的，“八亿神州尽舜尧”当然好，但前提“舜尧”们都可以活得很好。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp;&nbsp; &nbsp;孔庆东其余的话我不想再去说了（如生命的价值问题等等，我觉得他不具备谈这个问题的资格）。他的话的确让我震撼，我震撼这样的学者在北大对社会精英进行着怎样的一种教育，不能简单地用无知或偏激来形容他，我甚至觉得他有些精神分裂的倾向――我的确是这样想的。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 很多事情已经过去了，很多事情还会发生，我们不能再用“他的错误是一个伟大的革命家的错误”之类的话来解脱政治。我知道我无法去具体地说服孔庆东――哪怕果然有面对面的机会，我也无法说服他，虽然我的内心无比确切地知道他的观点是多么多么多么地荒谬。<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp;&nbsp;&nbsp;有一点我想是肯定的，孔庆东这样的人象苍蝇一样不可能消失，但也象苍蝇一样会随着社会的进步而越来越多地远离人们健康的生活，社会进步其实不是要彻底消除哪个物种，这一点上孔庆东可能会觉得很遗憾――尽管他其实也存在被消灭的危险。</div>

							
							
							
							
