<br />
上周日在北航西门乌有之乡书吧听了孔庆东漫谈金庸，假武侠之名而谈革命之实，嬉笑怒骂，果然不失醉侠风范。有个女生提问，声称非常喜欢章诒和那本书，看了不下5遍，想听听孔的评价。孔庆东立刻就说了以下的话来。言语之间虽然语气严厉，观点有点偏激，但一针见血指出章的虚伪和矫情之处。<br />
　　提问：<br />
　　 那本畅销书读过不下五遍，非常喜欢，但是对这本书的评价，你提到他的语言，你认为在那样的生活艰难里面，他崇尚非常高的生活，有什么不对吗？<br />
　　回答：<br />
　　 你说“那样的生活艰难“，非常好，谁生活艰难？我要问这个问题。康同璧生活艰难吗？不艰难，艰难不艰难是要比的。当时不知道你们家过的什么样生活，我们家过的生活是吃馒头非常罕见的，吃窝头也不一定能很好的吃，因为没有菜。我们家还生活在城市里，还有很多人捱饿，我们国家欠了那么多外债。在那种情况下，他们家过的非常好，吃腐乳都要吃二十多种。他那个阶级是我们政权的敌人，那个阶级过的那么好，共产党对他们的政策是极其宽大的，他们继续过着很奢侈的生活。共产党对他们这么好的情况下，他们仍然梦想变天，他们时刻梦想着骑在人民头上，后来拼命要翻案，说当年反右反错了。你当年是不是真实的反党？你如果当年确实反对共产党，把你打成右派一点不冤枉，是对的，你应该站起来说：我就是反对共产党，我是堂堂正正的英雄，为什么要求共产党平反？你不是反党，那你跟党的观点是一致的，可是你现在跟党的观点不一致，现在拼命说革命是错的，50年代，文革，错的，给你平反又说对的。你这人格有问题啊，这人格放在资产阶级里也是卑鄙的啊。我说此书写的好，说他感情真挚，真的留恋那样的生活。书里说你们家毛巾这么脏，毛巾要每天换一条，这才是人过的生活，那我们工人农民解放军过的都不是人的生活，我们一年能买几条毛巾，我们家毛巾买不起，我们家毛巾是工厂发的，是社会主义给的福利品，一年发两条毛巾，我爸发两条，我妈发两条。而他说毛巾要天天换，不然过的不是人的生活，床单是每天一换，洗的很白。书里边赞美很多东西，都是不自觉流露出来，在我看来都是有问题的。比如说史良的婚姻是多么好的爱情，周总理关心她，给介绍的小白脸，原来是上海巡捕房的，岁数小很多，晚上到卧铺睡觉了，小丈夫拿着小箱子，两个钉子，一个小帘，在卧铺钉两个小钉，一挂，自己坐在火车旁边，望着外面茫茫的原野，说这是爱情，这是爱情吗？我读到这里万分恶心，这怎么叫爱情？这种生活当然是你的自由，有那个条件，没人管，但有什么可炫耀的？当时我们过这样的，你们过那样的，不错啦，在这样的情况下，他们天天说这个党不好。要斯大林那样的党，就枪毙，到斯大林那儿，全部要消灭。我们中国是有人道主义的国家，关键是思想的问题，解放以后什么都没有变，他们的孩子坐汽车，我们的孩子雨里泥里爬着去，他们还欺负我们。那些大右派，看见漂亮女孩子就跟着人家走，一旦出了事，一旦反右运动开始，他们这些人互相出卖，陷害，人格丧尽。从共产党的角度来讲，反右是一点错误没有，从他们的角度来讲，反右也没有错误，有什么错误？你就是右派嘛，要推翻执政党，对政权构成威胁，不应该批评你？批评你是轻的，已经宽大了，你说错误，关键站在什么立场，你说中国今天是好时代，坏时代？没法说，如果家里有亲属刚刚在矿井上砸死的，跟我的立场就不一样，刚刚吃完十万块钱大宴的人，立场也不一样，中南海的人立场也不一样，即使在五十年代，大多数人生活比较一致的时候，立场也不一样，有的人认为这个国家欣欣向荣，有的人认为这个国家很艰难。怎么艰难，就认为不如他当皇帝好，不如他在紫禁城里，随便杀人那样好。作者是真诚的，他没有掩饰自己的心情，但他那种叫艺术吗，我可能跟他的艺术观不一样，他们建立在别人血汗之上的那种东西叫艺术，还是凡高、齐白石那种叫艺术？他们那种东西是靠金钱堆起来的。<br />
　　 中国人有钱了，又兴起了收藏热，电视台的那些编导都开始收藏了，他们买齐白石的画，但他们看墙上的画时，会为艺术而心灵震动吗，他们看的是钱，是150万或300。他们这些人是对艺术的一种糟蹋。真正倒霉的右派是有的，是小右派，比如一个中学老师，一个普通的教员，在单位里说了一句错话，正好别人要陷害他，他和别人关系不好，给他打成右派，以后平反了，只是摘掉一个帽子而已，生活一辈子被耽误了，倒霉的永远是小人物，倒霉的是下层人。金庸小说里写了：兴，百姓苦；亡，百姓苦。改革开放之后，都平反昭雪了，大右派百倍疯狂地向人民索取，比当年凶恶十倍。我们的血泪谁去写？在矿井下砸死60多人，谁给每人写一部《往事并不冒烟》？他们一个人死了赔多少钱？生命都是有价钱的，上层人的生命价格和下层人的生命价格就是不一样，革命本来就要改变这个东西的。<br />
　　 当然革命不可能一帆风顺，特别是在这样的一个时代，但是正因为革命不容易胜利，不是我们以前所想的正义总是压倒邪恶，正因为经常是邪恶压倒正义，这才是我们生活的意义。写作和工作，其实都是人类的一种对宿命的反抗，由此我们获得一份生命的意义。<br />
　　我们如果不怎么努力的话，正义不容易到来，我们必须肩住这个闸门，阳光才会射进来。<br />
　　所以我也到处向人们推荐这书，只是我推荐的角度是不同的啊。</div>

							
							
							
							
