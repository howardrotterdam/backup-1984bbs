<br />
2010年4月24日，巴塞罗那，萨马兰奇家族发言人胡安&amp;#8226;费尔南德斯在律师陪同下向新闻界公布了萨马兰奇遗嘱，据悉遗嘱订立于2009年3月，分为家事遗嘱和公务遗嘱两部分，公布于众的是公务遗嘱，全文约4万字，涉及到萨马兰奇对国际奥委会的感谢和回顾，以及对未来奥林匹克运动的展望，并且回击了对其“使得奥林匹克运动过于商业化、职业化”的批评，其中与中国有关的部分摘录如下：<br />
<br />
1980年7月16日我从基拉宁勋爵手中接任奥委会主席的时候，中国正处在复苏的初期，我曾经对尊敬的同事们说：“既然同为GCD国家的苏联可以举办一次成功的奥运会，那么中国也可以，我们没有理由无视一个占世界总人口五分之一的国家的存在。”那时的中国给我们的印象只有乒乓球和神秘感，有人甚至预言台湾举办奥运会的可能性都要大于中国大陆。因此我们当时的心情犹如使徒保罗刚刚进入罗马时一样。<br />
<br />
时至今日我可以自豪地说，中国已经成为一个巨人，至少在奥林匹克运动看来是这样。那些用业余体育论来批评国际奥委会的人应该在过去的那个夏季看到，中国是如何由国家培养竞技职业运动引导和带动业余体育运动的，故宫红墙下打太极拳的中国老人给所有国际奥委会成员都留下了深刻印象，这是一个古老国家体育精神的复苏。<br />
<br />
以商业化和职业化抨击奥林匹克运动的人应该看到，正是在允许职业运动员进入奥运会之后，奥运会才摆脱了财政危机和日渐式微的衰势，才能成为真正的世界第一运动赛事，才能走入千家万户的电视屏幕并鼓励电视机前的每一个怀有梦想的孩子投身球场和田径场。也正是奥运会带来的巨大的消费刺激才使得众多国家有了举办的兴趣。<br />
<br />
不可否认的是，奥运会现在过于庞大和臃肿，但是我相信这是令人庆幸的烦恼，抱歉的是我把这个烦恼丢给了罗格先生，每届耗资数百亿美元的奥运会确实开始令很多中小国家望而却步，这也的确违背了奥运会的初衷，但是，还有什么比预算不足的奥运会更糟糕的呢？<br />
<br />
奥运会需要保持它的影响力，这种影响力能促使人类体育事业的发展，也许我们可以换一个头脑，在全球化的今天，奥运会的举办地已经不很重要，它的任何讯息都能够通过卫星和光纤迅速传遍世界每个角落，从北极到罗得角，从喜马拉雅到死海。因此我建议可以适当修改“奥运会轮流举办”的原则，寻找一个合适的永久举办地，玫瑰碗橄榄球比赛永远在一个固定的场地举办，可是谁能否认它的伟大？<br />
<br />
2008年给我最大的感触就是――世界上没有任何一个国家能够比中国更能承担这样的一个荣耀，北京奥运会的空前成功、10亿人的高涨热情令人沉醉目眩，当听说北京郊区的农民用豆浆和牛奶灌溉蔬菜提供给各国运动员的时候，中国的热情足以让任何人感动。<br />
<br />
多达800亿欧元的资金被投入奥运会场馆建设，尽管这个国家人均年收入还不到1000欧元，但是每一个居民都行动起来，连北京的数以千计的乞丐也被动员起来前往昌平拉沙子，奥运会举办期间，连最偏远的数千公里外的县城都实行交通管制，我从来没有看到过如此团结的奥运会，他们（指中国人民）被有效团结在奥运组委会的旗帜下，自觉服务于奥林匹克运动，这令每一个在2008年夏季来到中国的外国人都感到惊讶。<br />
<br />
遗憾的是，2008年以来的金融危机似乎正在侵蚀奥林匹克运动，由英国ZF的态度可以看到，愿意维持奥林匹克运动进一步上升的愿望在缩减，对于2012年的伦敦奥运会我们只能保持谨慎的乐观，问题在于――伦敦之后呢？<br />
<br />
没有一个国家的ZF，能像中国一样以国库支持奥林匹克运动而不计较得失。<br />
<br />
没有一个国家的人民，能够如此以成功举办奥运为荣。<br />
<br />
没有一个国家，能够举国被自己运动员的金牌感动。<br />
<br />
更快更高更强，这句口号也适用于奥运会本身。<br />
