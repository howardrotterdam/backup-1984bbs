美国学者：中央集权更适合中国发展<br />
 2009-10-06 23:49:52 <br />
<br />
&nbsp;&nbsp;今天一大早，看到＜纽约时报＞的全球版＜国际先驱论坛报＞头版使用了「一次中国认为完美无缺的庆典」为标题，报导了昨天天安门广场的大型活动，认为这次成功的庆典，主要是向中国国内民众展示，而国际社会呢只是看看而已。其实，并非看看而已，我们看到一位美国学者对于新中国60周年的思考，他的结论是：中央集权更适合中国发展。这位学者，是美国＜亚洲协会＞美中关系中心主任夏伟(OrvilleSchell)，这篇原文英语由路透社中文网翻译的文章写到：最近的一个傍晚，我漫步天安门广场，回想起半个世纪以来我与中国结下的不解之缘，以及中国在21世纪取得的令人瞩目的成就，不禁感慨万千。<br />
<br />
50年前，我开始在哈佛大学学习中国问题。当时的中国是“政治挂帅”，领导人们大力颂扬计划经济，政府管制深入社会生活各个方面。但中美两国间的敌对关系令我们这些“远观中国”的人无法实地考察。<br />
<br />
1975年，我终于踏上中国的土地。当时，毛主席仍健在，文化大革命尚未结束，阶级斗争仍旧主导着国家政治生活；私人汽车、私营店铺、广告和私人财产无处可寻。即使是我们这些来访的外国人也要老老实实穿着蓝色中山装，头戴解放帽，参加“学习班”，用四人帮撰写的小册子净化我们的资产阶级思想。在接下来的几十年里，邓小平提出了“致富光荣”等口号，大力推行个人激励机制。中国民营经济从毛泽东时代浴火重生，飞跃发展。这令我惊讶不已。与此同时，西方的市场原教旨主义者为中国的变化而欢欣鼓舞。在他们看来，中国领导人正敞开怀抱接受自由市场理念，从他们一度深恶痛绝的资本主义上帝那里寻求救赎。<br />
<br />
当时，西方认为共产主义将消亡、或将向相反方向演变的“历史终结论”一度甚嚣尘上，美国的很多政治传道士也积极宣传民主与资本主义，他们期望中国不仅能放弃政府对经济的管制，还能开放政治制度。<br />
<br />
当然，中国领导人并不吃这一套，经常批驳西方“干涉中国内政”，并且愈加坚持列宁式的一党执政模式。随着中国的经济活力与日俱增，并向现代化、全球化的方向发展，与中国的政治制度落差愈加明显，很多西方专家预测，这种冲突将令中国陷入困境。然而到头来，深陷经济危机难以自拔的却是美国和西方。<br />
<br />
与此同时，中国则呈现出一派全新的气象。在审时度势，制定了周密的政策后，中国正健步向前，果敢地迎接一个又一个挑战。乘胜利举办奥运会的东风，中国政府又开始了人类历史上最为恢宏的基础设施建设项目，推出的经济刺激计划取得累累硕果。现在，中国又向公共福利领域进军，并在发展绿色科技、可再生能源、提高能效等方面引领世界应对气候变化危机。这些相关领域必然成为新的全球经济的温床。<br />
<br />
总之，当今的中国充满活力、家底殷实，富于远见和领导力，发展动力十足。相比之下，美国则似乎冥顽不灵，政治体制从上到下都陷于瘫痪。漫步天安门广场，我脑中反复思考着这样一个看似矛盾的问题：为什么我们在美国如此信奉和鼓吹的民主制度和资本主义辜负了我们，而我们长期排斥的威权主义和国家管制经济却让中国得到了繁荣的发展？<br />
<br />
无论从理论上，还是从政治上，这都是一个令人震惊的现实：尽管中国坚持中央集权，政治改革驻足不前，但如果我们美国人不能尽快调整好自己的政治制度，那么最终在这个高速发展、科技发达、日益全球化的世界里，恐怕只有中国这样的国家才能独享进化优势，适者生存。<br />
<br />
（2009年10月02日 总编辑时间）</div>

							
							
							
							
