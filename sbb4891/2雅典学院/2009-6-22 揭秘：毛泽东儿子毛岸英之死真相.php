　　“我迅速跑出来看敌机轰炸情况，一眼就看到彭总办公室方向正着大火冒烟，立即迅速跑去，彭总办公室已炸塌，正着大火冒烟。看到成普满脸黑呼呼地跑出来，棉衣也着了火，我要他赶快把棉衣棉裤都脱了，躺在地上打滚，将火滚灭。 <br />
　　 <br />
　　“我问成普：‘你是怎么跑出来的？’成普说：‘听到飞机投弹声，就从你让我打开的窗户门跳出来的。’ <br />
　　 <br />
　　“我急着问：‘那两位同志呢？’成普说：‘他们往床底下躲，没有出来。’ <br />
