&nbsp; &nbsp;&nbsp;&nbsp;中国人民志愿军司令部作战处副处长、原沈阳军区参谋长、毛岸英牺牲的目击者杨迪著了《在志愿军司令部的岁月里──鲜为人知的真情实况》一书,这本由解放军出版社出版的著作介绍了毛痛失岸英的经过.&nbsp;&nbsp;<br />
　　 <br />
　　书的第56页记述了1950年11月13日，第一次战役后，38军军长梁兴初被彭德怀狠狠批评的过程。第58页，杨迪写道： <br />
　　 <br />
　　“会议中也发生了我想不到、也不可能想到的奇异插曲（可能也出乎参加会议的领导同志意料之外），就是正当彭总向梁兴初军长生气、批评梁后，与会领导同志都处在沉静严肃的气氛中时，随彭总来的那位年轻俄文翻译（我看他和我的年龄差不多，二十七八岁）却毫不胆怯地站起来，指着挂在墙上的地图说起来了。彭总坐着一句话也不说，既不制止他讲话，也不批评他，志司几位副司令也不制止他，各军军长低着头也不吭声。那位年轻的翻译，并不懂军事，我没有听明白他在讲什么，他说了一二分钟后，看没有人理会他，也就不说了。当时我觉得很奇怪，怎么一个年轻翻译会在志司党委召开的作战会议上，而且是在彭总生气的严肃气氛中，敢于随便说话呢？还没有人制止他、批评他？真怪！ <br />
　　 <br />
　　“会议开完后，我对丁甘如处长说：‘这个小翻译胆子真大，敢在彭总生气时，还在那儿说三道四。看来他还不懂党内和军内的规矩，这样重要的高级会议，哪有他讲话、发言的资格。他是谁？他是什么人？ <br />
　　 <br />
　　“丁甘如同志说：‘老杨，你就不要问，也不要打听了，我不会告诉你，其他的同志也不会告诉你的，以后时间长了，你慢慢就会知道的。’ <br />
　　 <br />
　　“我说‘哪有这么神秘？不让知道，不让问，就拉倒。’ <br />
　　 <br />
　　“丁甘如说：‘老杨，不要说气话嘛，我不能告诉你，这是纪律呀！’ <br />
　　 <br />
　　“我说：‘我不过随便说说问问，我一定遵守党的纪律。’一直到敌人飞机轰炸志司后，经过了一段时间，才知道在彭总指挥室内被炸牺牲的两名同志中，那一位俄文翻译就是毛主席的儿子毛岸英同志。当知道了他的名字后，对发生的一切就不觉得奇怪了。” <br />
　　 <br />
　　1950年11月中旬，驻在大榆洞的志司遭美机轰炸，志司马上规定了严格的纪律，其中就有拂晓后一律不准冒烟这一条。23日，志司得到情报，次日上午10点，麦克阿瑟将发动“圣诞节攻势”。解方参谋长召开志司各部门领导干部会议，“重申防空纪律，严格要求明早拂晓前，吃完饭都一律要进防空洞。”连彭德怀也不能例外，还专门安排洪学志拉彭到防空洞里下棋。 <br />
　　 <br />
　　杨迪的书中第293页继续写道： <br />
　　 <br />
　　“第二天（即11月24日）拂晓前，我派参谋分头去检查防空落实情况，我自己也准备到重点地方去检查，这时，邓华副司令员派人来找我，对我说：‘你到彭总那里去看看，看洪副司令是不是已把彭总拉进防空洞了？’” <br />
　　 <br />
　　杨迪到防空洞看见洪学智正拉彭德怀进防空洞，准备下棋。 <br />
　　 <br />
　　“趁彭总和洪副司令正在摆棋子时，我赶快跑去向邓副司令报告。在我路过彭总办公室时，看到烟囱冒烟，立即跑进里面去看看，房里还有三个人正在炒米饭吃。我只认识成普同志，那两位同志我只知道一位是彭总的俄文翻译，一位是才从西北调来的参谋，他们的姓名我不知道。 <br />
　　 <br />
　　“我问成普：‘老成，你们怎么还在炒饭吃呢？赶快把火弄灭。’ <br />
　　 <br />
　　“成普说：‘我们马上就走。’ <br />
　　 <br />
　　“说完我就向邓副司令的防空洞跑去。 <br />
　　 <br />
　　“拂晓后，敌人的飞机编队飞临大榆洞上空，也不绕圈子就投弹，第一颗凝固汽油弹正投中彭总那间办公室，敌机群先将凝固汽油弹和炸弹投下后，绕过圈来就是俯冲扫射，然后就飞走了。 <br />
