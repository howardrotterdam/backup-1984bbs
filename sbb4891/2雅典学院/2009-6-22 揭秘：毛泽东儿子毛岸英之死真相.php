　　 <br />
　　“随后,我迅速跑到彭总和洪副司令的防空洞,看到他们很安全就放心了.&nbsp;&nbsp;<br />
　　 <br />
　　“我急喘喘地向洪学智副司令报告：‘洪副司令,不好了,彭总办公室被炸毁了’.&nbsp;&nbsp;<br />
　　 <br />
　　“洪学智副司令急着问：‘里面的人都出来了吗？’&nbsp;&nbsp;<br />
　　 <br />
　　“我说：‘只有成普跳窗户出来了,还有两位同志没有出来.’&nbsp;&nbsp;<br />
　　 <br />
　　“洪学智副司令员一听那两位同志没有出来,就急了,洪学智喊着赶快派人抢救.&nbsp;&nbsp;<br />
　　 <br />
　　“我说：‘已调部队和医务人员抢救.’&nbsp;&nbsp;<br />
　　 <br />
　　“洪学智副司令很快向着火的房子跑去,我也跟着跑去.火扑灭了,那两位同志牺牲在里面了.洪学智副司令员很着急地说：‘这可糟了,这可糟了！”我听了莫名其妙,又不好问.洪学智副司令要我赶快去报告副司令,他去报告彭总.当邓华副司令等首长听了我的汇报后,都奔向那烧塌的房子,也很着急很悲痛地说：‘这可糟了,这怎么交待呀！’&nbsp;&nbsp;<br />
　　 <br />
　　“我仍是不明白彭总和其他首长们为何这样着急和悲痛.由此,我突然想起在11月13日志司作战会议时,彭总严厉批评梁兴初军长,大家都紧张,都不敢说话,我指地图稍微偏了一点,彭总就批评我.唯独那位俄文翻译,年纪轻轻的,在当时会议那样严肃的气氛中,敢在彭总面前说这说那,彭总没有说他什么,而只坐着不吭声,邓华副司令等首长也没有制止他说话.我想,这位同志大概不是一般的翻译.&nbsp;&nbsp;<br />
