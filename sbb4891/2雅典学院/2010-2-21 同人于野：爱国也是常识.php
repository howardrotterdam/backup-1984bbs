																											<div id="postmessage_487024" class="t_msgfont">第二个澄清，社会主义者乃至共产主义的最终价值仍然不是集体主义，他们的最终价值仍然是自由。但他们的区别在于对自由的内涵不同。首先他们认为自由是相对性的阶段性的，换句话说人类社会发展的历史就是不断打破枷锁获得更多自由的历史。你这里说的自由主义者认为自由的终点在形式上的人身自由，交易的自由，信仰的自由，只要群几之界分清就算是达到了自由。<br />
<br />
社会主义者认为这只是一个阶段性的自由，在其之后还有更高阶段的自由，那就是免于饥饿和匮乏的自由，由此需要积极自由，需要以集体的形式加以干预实现这种自由。<br />
<br />
