																											<div id="postmessage_486884" class="t_msgfont">爱国也是常识<br />
同人于野<br />
可能是为了暗示他们也会跟普通人聊天，或者出租车司机是他们唯一有机会与之聊天的普通人，中国文化人都喜欢引用出租车司机的话。与一般人写文章都是称赞出租车司机的口才和见识不同，这期《南方周末》上梁文道的文章《爱国不忘爱己》，描写了一个“逻辑奇怪”的出租车司机：<br />
那位司机从沿途不断的收费站开始骂起，最后数到公费开销的浪费与贪官派人截访无告民众的可恶，这社会几乎没有一件事能令他满意，自己则处处受到不公的待遇。但奇怪的是，话锋一转，他居然盼望政府早日武力解决台湾问题，还说要是美国胆敢介入，就要迎头痛击，让他们知道谁才是世界第一强国。<br />
你自己都自身难保，为什么还这样爱国？梁文道说，“西方的自由主义传统一直不大明白平民百姓的爱国热情究竟是怎么回事”。爱国似乎是一种非理性的愚蠢行为。但梁文道有一个比西方的自由主义传统更好的理论。他举了二战时期日本的例子，认为爱国其实是为了获得一点尊严。“融入大我的崇高可以消弭个人生活上那微不足道的缺陷。”然后梁文道表示自己“同情地理解这种尊严的追求”，但仍然希望他们最好还是能先去追求自己的生活再说。<br />
这个理论不能解释为什么那些在生活中很有尊严的人也爱国。实际上，有统计表明（Super Freakonomics），最爱国的那些人，也就是那些参加革命的人，那些选举的时候出来排队投票的人，和那些参加恐怖组织的人一样，并非来自社会底层。大多数黑社会成员出身于贫寒家庭。大多数罪犯出身于社会底层。但大多数最爱国的人出身于社会中层。他们往往受过平均程度以上的教育。他们不缺尊严。实际上，美国的爱国者比例绝对不比中国少。<br />
一个不走运的出租车司机不见得是最爱国的人，但他也爱国，而且原因跟别人没有什么区别。爱国是人的一种本能。现在我来分析一下爱国心理学。<br />
爱国心理起源于“认同感”心理，Identity 心理。美国德克萨斯州曾经一度被公路上被开车者从车中乱扔出来的垃圾所困扰。政府想了各种办法，比如说罚款，后来又在电视里播放教育人们爱护环境的公益广告，但是都不好使。德州的人都很有牛仔精神，认为跟警察对着干很酷，而且可能还认为女人才担心环境。最后起作用的公益广告，它的广告词是这样的：“真正的德克萨斯人不开车扔东西”。（此故事来自 Made to Stick 一书。）<br />
也就是说这个广告跟论坛上“中国人必看”，“是中国人就顶”这种标题党一样，利用了德州人以自己是德州人为荣的这个心理。人们就是这样不讲理地以自己的身份认同为荣。<br />
爱国思想的本质其实就是民族优越感，英文文言文叫做族群中心主义，ethnocentrism。那么为什么会有这种莫名其妙的认同感？这种认同感是非理性的么？这个心理学家可能不好解释的问题，可以用计算机模拟的方法解释。<br />
Brookings Institute 的 Ross Hammond 和密西根大学的 Robert Axelrod 在2006年做的这个模拟实验，现在已经成了学术界的经典。实验用计算机模拟一个世界上有这么多人，这些人每天随机地交往。交往的规则是如果双方都合作，那么双方都受益，但每个人都有可能通过欺骗对方来使自己得到更多好处。也就是说他们每天的交往都面临囚徒困境。如果每个人都没有任何信息来判断对方是否值得信任，那么显然这个社会的一切交往都只能靠运气。<br />
有意思的部分是这样的。程序完全随机地把全体人员分为四组，然后给每组人“刷”上一身颜色。设定其中每个人都可以给自己选择一个策略，利用这个策略来判断自己到底是选择合作还是欺骗一个人。同时，每个人都可以借鉴学习别人的成功策略，这样好的策略可以流传开来。可供选择的信任策略包括，<br />
- 1. 完全随机地决定合作还是欺骗<br />
- 2. 与跟自己颜色相同的人合作，欺骗跟自己颜色不同的人<br />
- 3. 与跟自己颜色不同的人合作，欺骗跟自己颜色相同的人<br />
实验结果是这个本来随机的颜色划分的确把人分成了不同的族群。一开始可能是几个同一颜色又恰好采用第二条策略的人偶然凑在了一起，他们这个小组因为总是互相合作而受益了。然后其他人会学习这个策略并加入这个小组，这样小组就会越来越壮大。只有这第二个策略会导致这种正反馈。最终，哪怕是那些采取“民族虚无主义”，也就是第一个策略的人，也混得不行了。<br />
这个实验相对于真实世界显然是特别简化的。但它说明了一个重要事实：民族优越感其实就是一个促进合作的机制。如果你在淘宝买东西，一家店主表示自己很爱中国，另一家店主表示自己不爱中国，而你恰好是个中国人，请问你更信任哪家店呢？<br />
多年进化的结果，这种合作与欺骗的爱国算计很可能已经进入人的潜意识，成为感情的一部分，甚至变成了一种原始的冲动。当我看到中国队跟别国比赛，我本能地就支持中国队。这跟崇高可能没什么关系，就好像看到美女产生的原始冲动一样。<br />
现在回到那个被梁文道“同情”的出租车司机。在这里，文人“奇怪”他的原因可能不是他希望打台湾，而是因为他在国家对不起他的情况下还爱国。但科学事实是爱国是一种原始冲动，跟国家对不对得起自己没关系。这就好比说爱不爱美女跟自己想不想要孩子没关系一样。<br />
正如美女令我们产生的原始冲动有可能导致犯罪，爱国这种原始冲动也有可能导致军国主义和种族歧视。但是你不能说爱国很奇怪，正如你不能说喜欢美女很奇怪。<br />
在这个连同性恋都理直气壮的时代，爱国有什么不能光明正大的呢？</div>

							
							
							
							
