																											<div id="postmessage_487008" class="t_msgfont"><strong>爱国与宗教</strong><br />
<br />
<strong>一、爱国与宗教的迷茫（缘起）</strong><br />
<br />
　　最早模模糊糊地意识到爱国与宗教的关系是在上初中的时候。那天我在一个旧书摊花了三元钱（当天的午餐）买了一本旧书。就是郭沫若那厮写的《李白与杜甫》。里面的观点少有可观者，顺着本朝太祖的爱好刻意地贬杜扬李，那卑躬屈膝的态度已十足让人恶心。不过桀纣之言未必无可观者，里边有一段关于李白作道士受符的话给我以很深的印象。虽然印象很深，原文也是记不住了。大概是说作道士受符（大概相当于佛教的剃度）时，要站在老君像前很长时间。所以人容易精神恍惚产生幻觉，以为见到老君如何如何。这段话使我想起了升旗仪式和祭扫烈士陵园时长时间的肃立。觉得其中有某些关联。但当时只是一闪念，就忽略过去了。直道数年后才偶然间再次想起，继续苦苦思索，人类的思维就是这样不可琢磨。<br />
<br />
　　小时候我愚钝得很，人家说什么我就信什么。刚入少先队时，人家告诉我：“红领巾是用烈士的鲜血染成的。”于是，发下来第一条红领巾我便在嘴里嚼个不停，想尝尝鲜血的滋味。（人类真的有嗜血的传统？！）小学祭扫烈士陵园，有一个必不可少的仪式，就是几个人抻着红领巾，在音乐但伴奏下，缓缓地围着烈士纪念碑转一圈。我当时颇疑心碑后面藏着几个烈士，拿着刷子给红领巾涂血。但又颇惋惜学校的马虎，怎么不拿条白色的领巾让烈士来涂，这样我也能看得更明白些。由于真的相信鲜血之说，后来在电视里又看到流血的恐怖画面，相当长一段时间里，傻乎乎的我对脖子上的红领巾颇为敬畏。现在想起来，是在傻得有点出格。但即使后来知道了自己的愚蠢，也仍旧养成了一种习惯，就是在升旗和扫墓这类分所的仪式中一边听着冗长的演讲，一面近乎虔诚的凝视着国旗或烈士像。这样不但能分散注意力，缓解腿部的麻木，还似乎真的能与国旗或烈士做某种超乎自然的交流。看了《李白和杜甫》之后才明白，原来又做了蠢事。朦胧中想起了原始人的鲜血崇拜。<br />
<br />
　　后来，做了一段民族主义愤青（愤少？！），又认真地反过一阵西方，啃了许多社会主义历史、前苏联小说以及日本的左派作家作品。在歧途上时而激昂，时而彷徨。直到9・11才开始渐渐从民族主义梦魇中苏醒，走上独立思考的路。我希望能通过这篇文章，与诸位分享自己的思考过程和看法，希望能够对诸君的思考有所助益，并希望诸君不吝赐教，对本文提出批评。<br />
<br />
<strong>二、爱国主义是一种宗教</strong><br />
<br />
　　我认为，形形色色的宗教虽然教义不同，源头各异，但究其根本，都是教人畏、恨、爱的理论。早期宗教教人畏惧、仇恨的东西较多，教人爱的东西很少。<br />
<br />
　　无论东方西方，每一种宗教的神祉都是高高在上不可侵犯，且有令人畏惧的神力。要是对它不敬，若非现世遭报，定是死后清算。这是宗教之畏。<br />
<br />
　　同样无论东方西方的宗教，都有魔鬼或其类似物存在，作为教众憎恨的对象。在早期宗教中，这种憎恨不需要理由，而且成为检验教众虔诚的一种方式。这是宗教之恨。<br />
<br />
　　至于宗教之爱，则是对教友的关怀。这种爱的正面效果直到宗教改革以后，才逐渐凸现出来，并且成为西方民主中“博爱”原则的基石。但在早期宗教中，这种爱不过是加强凝聚力的一种手段。即对待教友像春天般的温暖，对待异端和异教徒要像秋风扫落叶一样冷酷无情。早期宗教的爱，最终仍旧落在恨上，成为打击异己的一种工具。平素慈祥和蔼的老大娘一转眼就会争先恐后的往布鲁诺的柴堆中添柴火，上午给你春天般的温暖，中午你成了异端，下午就给你秋风扫落叶般的冷酷无情。<br />
<br />
　　宗教是这样，爱国主义也是这样。<br />
<br />
　　爱国主义也是一种教人畏惧、仇恨和片面的爱的意识形态。<br />
<br />
　　有人说爱国主义没有神像怎么能是宗教？其实爱国主义与世界上的一神教和多神教是有些不同的，就是它确实没有明确的神。但是，爱国主义却有“图腾”，即比早期宗教更加原始野蛮的“图腾崇拜”的特征。爱国主义的图腾是让人畏惧的，因为它们是国家的象征。反对之、亵渎之，无须待幽冥烈火，今生即可受尽折磨兵祸延子孙。爱国主义是教人仇恨的，棋手很对象外有敌国（异教徒），内有“汉奸”（异端）。爱国主义自然也是划定了爱的范围的，即爱国、爱政府、爱意识形态、爱同为爱国者之同胞。当然这些爱是为了更有效打击其所恨的对象。既不是现代基督教的博爱，也不是墨子的兼爱。<br />
<br />
　　爱国主义也与其他宗教一样是排他的，唯我独尊的。它不会与任何事物分享权威。没有人可以即是基督教徒又是穆斯林。当然也不会有人即是爱国者又是其他宗教的虔诚信徒。有人说，不时有爱国的宗教么？不是说宗教也应该爱国么？其实所谓爱国的宗教不过是爱国主义披上了一层宗教的外衣而已。当两种宗教发生了矛盾，便发现了其本质。例如，全世界的天主教都是受罗马教廷管理的。但是在爱国主义国家范围内的所谓的“天主教”则拒绝教皇权威。遇到教廷利益与国家利益发生矛盾时，还会站在官方立场上把教宗骂个狗血喷头。所以这根本就是经过伪装的爱国主义。<br />
<br />
<br />
　　马克思主义对宗教的特征有更学术化的描述。马克思主义认为宗教具备如下特征：<br />
<br />
　　第一，非实践性。它不是在实践的基础上，由感觉器官对外部事物的直接感受而产生的感性认识，也不是对来自实践的感性材料进行概括而产生的理性认识。<br />
<br />
　　爱国主义不是一种理性认识，我想这一点当无疑义。而且爱国主义同其他宗教一样，也不是一种建立在实践的基础上的。和宗教一样，真要是一板一眼的按照他们所说的去实践、去研究，肯定露馅。舍身饲虎之类的神迹只能说说，真的认为能成佛，那只能变成虎粪。至于爱国主义也好似一样的。日本试过一回，说吞并满洲侵略中国就能统治世界。德国也试验过，说灭绝犹太人就能统治世界，结果统统被打回原形。更典型的例子是义和团的师兄们。满怀爱国主义激情，放弃反清复明的私仇，要扶清灭洋。喊着刀枪不入冲向战场，结果适得其反。今天的粪青们抵制日货的口号喊得震天响。吓得吉野家快餐店漏出港资的底。这下大家才知道它不正宗。几家正宗的日本快餐顺势登陆中国。爱国主义也是实践不得的。为了鼓舞所谓民气他们只好靠造谣诬陷等卑劣手段实现其自以为高尚的目的，如著名的罗刚事件等<br />
<br />
　　第二，非理性。宗教观念在本质上是同理性对立的，是压抑理性和反理性的。<br />
<br />
　　爱国主义是一种非理性的感性认识。爱国主义并没有对感性认识加以理性分析。而是倡导一种非理性的爱国、不需要理由的爱国，甚至连什么是爱、什么是国都没有搞清楚就大谈爱国。爱国讲求一种民气。无论付出多大的牺牲、多大的代价，造成多大的罪孽，也要捍卫一种虚无飘渺的东西――国家尊严。<br />
<br />
　　第三，非批判性。首先，宗教的信仰对象是神圣的，具有不可反驳性。它只要求对它绝对服从、虔诚崇拜，而不许对之提出异议。对它的任何批判性思考，都被看作是对神的亵渎，都是与神圣的宗教感情不相容的。它对人们的最高要求，就是虔诚地信仰和不加思考地崇拜。其次，宗教命题和教义被赋予了“绝对真理”的性质，并在现实中成为规范人们行为的准则。如果对它们提出反思以判定其正确或谬误，对虔诚的教徒来说，那简直意味着发疯。最后，由宗教观念和行为现范综合成的宗教体系，也被赋予了万古不变的永恒性。这种封闭的体系顽强地抵御着外界变化对它的影响，禁止人们用批判的眼光反思自身。<br />
<br />
　　这第三点更是符合爱国主义的宗教本质。国家及代表国家的图腾（从国旗、国徽、国父到奥运吉祥物）的神圣不可侵犯性。爱国主义禁止对国家权威的怀疑和反思。任何理性言辞都被看作对国家神道的亵渎。它要求人们无理由、无条件的爱国。如果谁对国家及图腾提出质疑和轻视，则会被视为叛国者和汉奸。对虔诚的爱国者来说，这是不可思议的。<br />
<br />
　　其他宗教有神明，爱国主义有图腾；其他宗教有经典，爱国主义有头脑发热的宣传手册；其他宗教有圣歌，爱国主义有革命歌曲；其他宗教有宗教仪式，爱国主义有升旗、降旗、扫墓；其他宗教有布道，爱国主义有国旗下的演讲；其他宗教有圣徒，爱国主义有烈士；其他宗教重苦行，爱国主义重牺牲……其实爱国主义那里是什么主义，分明应该叫做“爱国教”。<br />
<br />
<strong>三、爱国主义是邪教</strong><br />
<br />
　　现代社会大抵是标榜宗教平等、宗教自由的，但是有一种宗教却在格杀之列，那就是邪教。邪教是不是宗教？我认为是的。为什么叫它邪教？我认为，那是因为邪教已经越过了现代文明能够容忍的底线，即政教分离。宗教不得干政，即不得利用宗教权威，以神权的名义，鼓动非理性的教众干涉国家内政外交和正常的社会秩序。当然，社会也会保护宗教在不影响他人自由的条件下的合法宗教活动。<br />
<br />
　　可以说早期宗教如果以今天的标准衡量，基本上都是邪教。中世纪的天主教大搞宗教恐怖，把被诬为巫师的妇女和违反教廷法律的人烧死。再早一些充满了血腥杀戮的十字军东征，都充分的告诉我们早期天主教的邪教本质。这是违反民主的基本思想和自由的人类天性的。<br />
<br />
　　早期宗教经过一系列改革才蜕变为现代宗教。最著名的就是欧洲与文艺复兴齐名的宗教改革运动。<br />
<br />
　　经过这场改革，淡化甚至去除了宗教里关于仇恨的鼓动，代之以全人类的博爱思想，宗教改革和政治上的民主革命一样，是人类进化、人性发展的里程碑。我认为，现代宗教经过改革已经成为一种单纯的、给人以慰藉的，成系统的神话。<br />
<br />
　　被历史遗弃的邪教具有以下特点：鼓吹政教合一、以教代政；鼓吹教徒通过流血牺牲完成对宗教的皈依；极度仇视异教徒和异端；为达目的不择手段，为了自以为神圣的目的不惜逾越任何底线。一个宗教符合任何一条都可以被判定为邪教。细心些的朋友大概可以发现爱国教完全具备这四条特点。<br />
<br />
　　因为爱国的对象是国家，国家本是概念上的东西，想要具体化，只有通过以政府为主导的国家组织，虽然爱国主义粪青们总是说“爱国家并不等于爱政府”。但是，国家和政府有什么区别他们从来都说不明白。而且一到具体事例就露出了尾巴。比如提倡民主，攻击政府弊端，他们不乐意；说奥运吉祥物难看，他们不乐意；攻击本朝太祖，他们不乐意。最明显的是，他们拜的图腾仍旧是政治上的象征五星红旗。无论是爱国主义球迷，还是反日粪青，他们身上涂的，手里挥舞的都是国旗。所以爱国教的祭司就是主管宣传的政府公务员们。大祭司就是国家领袖、政府首脑。爱国教是政教合一的。<br />
<br />
　　爱国主义同早期宗教和一些现代邪教一样，鼓吹通过流血牺牲达到对宗教的皈依。什么抛头颅洒热血，动不动就鼓吹动武，提倡所谓的尚武精神。把在爱国主义斗争中牺牲的人称为烈士。<br />
<br />
　　爱国主义也同所有邪教一样，极端仇视异教徒和异端，即敌国公民和本国理性人士。对敌国公民，进行整体的妖魔化。说日本人淫荡，日本人是倭猪。你要跟特较真，他当然会说我没有针对所有的日本人民，但这是他们与理性人士论战时的说法。在对普通民众的宣传鼓动时绝对不会加以任何区别。对本国理性人士则会冠以叛国者的恶名，在中国，则名之曰汉奸。虽然他们根本从来没有出卖什么国家利益、主权、机密，也大抵没有机会接触任何机密。但是却并不影响爱国者们称其为“汉奸”。而且造出种种谣言，说理性人士受敌国雇佣，发表攻击本国政府的言论，麻醉人民。用谣言和欺骗等无赖手段诋毁异端威信。<br />
<br />
　　爱国主义为了实现其目标也同样鼓吹不择手段，逾越任何底线。9・11对他们来说不是一个灾难，而是一个给他们以巨大鼓舞的事件。他们鼓吹超限战。他们有句口号赤裸裸地暴露了他们的邪教本质，就是：“宁可台湾不长草也不放弃台湾岛”。当然，在祭司们没有引爆爱国主义情绪前，他们仍旧是夸夸其谈的无胆鼠辈，只能做出恶意拨打日资企业800服务电话之类的超限手段。<br />
<br />
　　爱国主义排斥其他宗教和权威，把一切不受他们掌控的权威看作对自己的威胁和挑战所以爱国主义又是一种一神教。在加上上面的一系列分析，可以确定。爱国主义是一种鼓吹政教合一的带有原始野蛮的图腾崇拜性质的狭隘的具有极大隐蔽性和欺骗性的一神邪教。<br />
<br />
　　所以现在国际上的许多争端并不是什么文明冲突论，什么东方文明西方文明之间的矛盾。而是和伊斯兰教原教旨派与现代西方的矛盾一样。是政教合一的野蛮邪教与政教分离的现代文明世界之间根深蒂固的不可调和的矛盾。在这个全球化的世界，解决这种矛盾只有两种途径1.文明世界的沦丧。2.邪教通过改革自身融入现代文明。<br />
<br />
<strong>四、邪教的救赎</strong><br />
<br />
　　这么多人受邪教影响怎么办？这么巨大的威胁怎么化解？<br />
<br />
　　历史告诉我们，思想问题只能用思想手段来解决。从来没有过任何思想问题是可以用专政手段、法律手段成功解决的。解决爱国邪教的威胁，也只能够用大禹治水的办法，一个字：“导”。<br />
<br />
　　中外都有使早期有邪教倾向的宗教成功通过改革转变为平和的现代宗教的特点。值得我们借鉴。<br />
<br />
　　1、以路德教改革发端的欧洲宗教改革<br />
<br />
　　欧洲的宗教改革是以路德教为发端的。路德教的核心就是反对教会权威。认为人们不需要通过教会，每个人都可以直接通过圣经与上帝交流。否定了独此一家、别无分号的基督教理论和政通教会的存在。这也为其他新教的诞生提供了依据。<br />
<br />
　　爱国教也可以用这种方式加以改革，即每个人都可以用自己的方式爱自己的祖国。不能说反日就是爱国，友日就是卖国。或反美就是爱国，亲美就是卖国。也不能说维护现行政治制度就是爱国，追求民主呼唤革新就是车国家后腿的卖国行为。每个人都可以以自己的方式与国家神道交流。每个人都可以以自己的方式爱自己的祖国。没有什么权威教会和固定标准。爱国是每个人的私事，像房事一样，外人无权说三道四。<br />
<br />
　　通过这样的道路，爱国可以成为一种正常的宗教情感和行为。<br />
<br />
<br />
　　2、王重阳的全真教对宗教的改革<br />
<br />
　　中国别看在许多方面非常落后，但是在宗教改革上却走在了世界的前列。在宋代以前，中国也有过鲜血淋漓的宗教斗争，虽然牵扯面不像欧洲那样大，但是也是刀光剑影，血泪斑驳。知道全真教的建立和传播。<br />
王重阳的全真教并不是单纯的道教，而是儒释道三教合一的宗教。他以道德经、金刚经、孝经为经典，拜老君也拜孔子还拜佛陀。全真教的流行助长了东方泛神秘论思想的壮大，形成了有中国特色的宽容宗教观。无形化解了许多宗教纷争。<br />
<br />
　　爱国主义的改造也已可以借鉴古人的经验。其实中国文化是世界上源头最多、最杂的文化。特别是从汉唐到宋元这一段，是中国文化最开放的时代。许多异族异域文化被吸纳入汉文化的范畴。中国人应该恢复开放的古风，进行文化多元化的尝试。前些时候举办的法国文化年，就是一个很好的引头。如果能够进一步介绍普及法国文化知识，可以让狂热的粪青知道世界上并不是只有一个光辉灿烂的中国文化，至少还有一个同样神圣伟大的法国文化。在结合黑格尔―马克思主义的辩证法，并加强世界史的课程内容，让青年人学会从多角度考虑问题。这样会使他们的心胸更开放、更宽容。爱国主义中的偏执毒素会渐渐消于无形。<br />
<br />
　　只有当爱国主义（爱国教）成功的蜕变为现代宗教，才能成功地见容于现代社会。成为有资格享受宗教自由权利的一分子。<br />
<br />
　　怎么区别平和的爱国宗教和狭隘的爱国邪教？很简单，三个字：“不干政”。即，我们不反对任何人以自己的方式热爱自己的祖国，但是不可以以爱国的名义煽动风潮，影响国家的内政（如选举）、外交（如战争）。当然对选举和战争人民有最高决策权，但必须以理性的思考、利益的衡量去抉择。而不是以爱国为名义的狂热。爱国，可以用建设祖国、整理国故等其他方式进行。而不是用那种群体的愤怒来干扰社会秩序。否则，爱国依旧还是邪教。<br />
<br />
