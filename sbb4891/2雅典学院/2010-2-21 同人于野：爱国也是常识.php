																											<div id="postmessage_487087" class="t_msgfont">爱因斯坦为何不爱国<br />
<br />
2006-09-07 09:13:27　来源: 南方报业　 网友评论 47 条 进入论坛<br />
<br />
作者：王永章 <br />
<br />
2005年是爱因斯坦发表相对论100周年。早在年初，德国政府就已启动“爱因斯坦年”庆祝活动，一场“爱因斯坦热”在全德掀起。不过，还有3个国家也在跟德国争夺爱因斯坦的归属。那么，爱因斯坦究竟属于谁呢？<br />
<br />
爱因斯坦是个出生于德国的犹太人，在瑞士首都伯尔尼完成并发表相对论，后为躲避纳粹迫害而加入美国籍，最终殁于普林斯顿。从种族、出生、工作和移民四个角度看，爱因斯坦的确可以分属以色列、瑞士、德国和美国。争夺爱翁“归属”，四国各有其理。<br />
<br />
按照传统观念，当说某人是某国人时，一方面是指他拥有什么国籍，另一方面是指他属于哪个民族。这是一个政治问题。可是，爱因斯坦在拒绝出任以色列总统时说了一句话：“方程对我而言更重要些，因为政治是为当前，而一个方程却是一种永恒的东西。”所以，爱因斯坦属于科学，而科学是无国界的。<br />
<br />
科学家有祖国吗？对爱因斯坦来说，既有也没有。德国是他的祖国，但当纳粹把它自己凌驾于国家之上时，爱因斯坦逃到美国，并上书罗斯福总统，建议研制原子弹，目的在于防止希魔抢先掌握威力无比的杀人武器。可见，在爱因斯坦看来，被纳粹政权奸污的德国，已经不是他的祖国了，爱翁没有“祖国”。<br />
<br />
爱因斯坦是美国人吗？一个拥有美国国籍的人，怎么又参加反对美国的核战争的和平运动呢，他这是爱国吗？这牵涉到一个常识：爱国不是爱某个政府，更不是爱某个政党。反过来说，反对某个政党或政府也不是不爱国。因为不论哪个政党或政府，都不能等同于国家或祖国。比如，没有纳粹时德国人就没有国家或祖国吗？况且，谁都不是神仙，都不可能不犯错误，怎么就不许反对呢？正像爱因斯坦自己所说的：“在真理的认识方面，任何以权威者自居的人，必将在上帝的嘻笑中垮台。”<br />
<br />
我倒觉得，最有资格将爱翁“归属”于自己国家的，当属瑞士。爱因斯坦说过一句话：“最重要的宽容就是国家和社会对个人的宽容”。他的相对论完成并发表于瑞士可能不是偶然的。换句话说，与瑞士这个国家和社会对个人的宽容是分不开的。不说瑞士在两次世界大战中都保持中立，瑞士还是宗教改革的发祥地，三大宗教改革家加尔文、茨温利和马丁路德，都长期在瑞士与旧势力进行了激烈的辩论，并留下了光辉的篇章。没有瑞士这片宽容的土地，新教的产生也许就是很难说的事了。<br />
<br />
就像数学大师陈省生弥留之际说出“希腊”二字，可能意味着希腊是数学的发祥地，他应该“归属”于希腊一样，爱因斯坦究竟属于谁这个问题，恐怕只有爱翁自己最清楚。<br />
<br />
1955年4月18日逝世于普林斯顿的爱翁，留下了这样的遗嘱：不举行任何丧礼，不筑坟墓，不立纪念碑，骨灰撒在永远对人保密的地方，为的是不使任何地方成为圣地。可是，当今世界，几乎没有看不到爱因斯坦画像或雕像的国家或大学。你说，死后不留任何痕迹的爱因斯坦，究竟属于谁呢？<br />
