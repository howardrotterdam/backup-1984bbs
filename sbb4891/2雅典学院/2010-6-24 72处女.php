													<h2>72处女</h2>
						
						
																											<div id="postmessage_702271" class="t_msgfont">问：香港凤凰卫视评论员最近在“评靖国神社话题”中说穆斯林的圣战是为了“能得到天堂里的仙女”，请问伊斯兰是这么认为的吗？<br />
<br />
<br />
答：西方一些“学者”认为，伊斯兰教规定参加“圣战”牺牲者均为“烈士”，而烈士都能进入天堂。天堂里不仅有永不枯竭的物质享受，还有纯洁的美女陪伴。正是由于伊斯兰以这样的物质和精神享受诱惑贫苦的穆斯林，才产生了渴望献身的“恐怖分子”。例如泰国一位自称为“圣战专家”的人，在接受该国《亚洲时报》记者的采访时就说：“穆斯林的游击队员在即将牺牲时，古兰天堂的仙女会降临人间带走他们的灵魂。他们在天堂可以得到72个美貌胜于人间所有美丽加起来的仙女。我曾经研究过在克什米尔战斗的巴基斯坦穆斯林游击队员的600个愿望，几乎都离不开上述思想。所有的穆斯林游击队员都提到这些仙女是他们发动‘圣战’的一个重要诱因。住着仙女的天堂是他们的首要目标。”[1]<br />
<br />
我们且不去说这段言论中有多少不合伊斯兰常识的词语，也不追查“圣战专家”是否真正做了这样的调查，调查结果是否如实，仅就穆斯林为了得到天堂的仙女而盲目战斗这一点而言，其表述或者是完全不了解伊斯兰教义教法，或者是有意歪曲伊斯兰原则。<br />
<br />
首先，伊斯兰教所说的“烈士”，是在反抗迫害、抗击侵略、解救无辜、防暴平乱等“为主道而战”的斗争中捐躯的穆斯林将士。倘若战争性质不是“为主道而战”，而是为着发动战争者的团体的或个人的私利而战，那么，参加这场战争的任何一位穆斯林，都不仅不能承认是“伊斯兰烈士”，而且是违犯安拉关于“枉杀无辜”禁令的罪人。正因为如此，每个穆斯林对待所有群体行为，不能人云亦云，随意盲从，而要以安拉的启示和先知穆罕默德的教诲去分析、判断，如果自己缺乏判别是非的能力，应当倾听精通经训的阿訇学者的意见，看看绝大多数人的行动，特别是在参加直接涉及他人生命安危的暴力斗争时，更应该慎之又慎。<br />
<br />
其次，伊斯兰讲求动机和结果的统一。伊斯兰教的先知穆罕默德明确指出：“一切行为，唯凭立意。一个人立意什么，便得到什么。”[2]一位穆斯林，就是参加了“为主道而战”的一场战斗，但如果其动机不是为了求得安拉的喜悦，不是为了“扬善止恶”，不是为着维护穆斯林大众的权益，而是一心为着满足他个人的私欲，那么他实际上就脱离了安拉的正道，就不能再以其亡命战场的结局判断其归属。据艾卜・胡莱赖说，他们攻克了海白尔[3],但是没有缴获金银货币，只得到一些牛、骆驼、用具和果园。后来他们和先知穆罕默德一同开往瓦迪・古拉。先知带着一个名叫米德阿姆的奴隶，是迪巴卜族的一个人送先知的。正当这个奴隶去卸先知的行装时，突然一支冷箭飞来，将其射死。人们说：“祝贺他以身殉道！”先知指出：“不！以主宰穆罕默德性命的安拉发誓，海白尔战役的战利品未经分配，他却私自拿走一件衣服。是此件衣服向他燃起了火焰。”听了先知此话，有人拿来一两根鞋带，对先知说：“这是我在海白尔战役中得到的。”先知便说：“这是一两根火鞋带。”[4]仅仅贪占了一点战利品，命丧战场就不能称之为“以身殉道”，那么一心一意为了满足性欲而参战，当然更不应按烈士对待了。<br />
<br />
第三，根据《古兰经》和先知穆罕默德遗训，穆斯林在具有正确动机并且在为主道而战的过程中受伤，是安拉喜悦的行为。先知曾讲：“安拉最喜爱，莫过于两滴血泪，两种遗迹：一滴怕主的热泪，一滴圣战的碧血，一种主道上的创举，一种天命的功绩。”倘若英勇作战，被敌人杀害，即为“烈士”。安拉在《古兰经》中启示说：“谁为主道而战，以致杀身成仁，或杀敌致果，我将赏赐谁重大的报酬。”这里的“重大报酬”即获准进入天堂。天堂是人人向往的的无限美好的后世乐园，是归信伊斯兰并且行善者的最终归宿，在那里可尽情享受安拉所赏赐的物质和精神的恩泽，包括美丽纯洁的伴侣（至于是不是“72个美貌胜于人间所有美丽加起来的仙女”，尚无确切的经训记载）。烈士既入天堂，当然也就有了享受美好幸福生活的资格。但是，天堂的享受，无论是物质方面还是精神方面，都不能与人世间的同类事物相提并论。因为，一方面天堂的物质财富和精神享受只对天堂的居民带来益处，而绝无任何副作用；另一方面，进入天堂的人都是灵魂纯净的优秀穆斯林，他们不再有任何邪恶的欲望，也绝对不会去追求什么背离天堂纯洁性的非分的感官刺激和物质享乐。<br />
<br />
总之，伊斯兰认为，为主道而战的“烈士”是尊贵的，其报酬是丰厚的，这些报酬只属于信仰坚定，道德高尚，乃至为了伊斯兰和平大业不惜牺牲自己生命者，与那些为满足个人邪恶欲望而企图投机的自私自利者毫无关系。“伊斯兰教义鼓励穆斯林为获得仙女而献身”，对于这样的谬论，就连对伊斯兰教并无多少了解的法国社会学家奥利维埃・罗伊（Olivier Roy）也不赞同。他在《全球化的伊斯兰：寻找新的乌玛》一书中批驳道：“那些认为伊斯兰式的自杀袭击是要在天堂赢得处女的说法是没有道理的，为什么穆斯林人只是在1983年才发现自杀袭击是进天堂的好方法？。”<br />
<br />
所以，如果有人以所谓可获得72个仙女的报酬诱骗穆斯林去参加他们的“圣战”，那么毫无疑问，这种“圣战”并非伊斯兰原来意义的“为主道而战”，组织这种暴力的组织也并非真正的伊斯兰性质，充其量只是打着伊斯兰旗号罢了。<br />
<br />
如果有人污蔑所有冲锋陷阵、不怕牺牲的穆斯林都是为了得到“72个仙女”，进而攻击穆斯林所从事的暴力反抗是非正义暴力即“恐怖主义”，如果不是头脑简单，轻信了别有用心者的谣言，就是出于某种目的制造谣言，甚至是真正的恐怖主义的帮凶或者他本人就是恐怖分子。</div>

							
							
							
							
