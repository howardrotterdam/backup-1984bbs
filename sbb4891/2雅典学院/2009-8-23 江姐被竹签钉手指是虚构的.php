													<h2>江姐被竹签钉手指是虚构的</h2>
						
						
																											<div id="postmessage_212020" class="t_msgfont">竹签钉手指是虚构 真实江姐到底受过啥酷刑<br />
<br />
2009年08月20日 人民网<br />
<br />
江姐，是在20世纪50年代后期、60年代初期因革命回忆录《在烈火中永生》和“党史小说”《红岩》的相继出版，而成为当代中国公众家喻户晓的革命烈士。<br />
<br />
她本名江竹筠，地下党组织内一般都称她“江姐”。1948年初，她的丈夫彭咏梧(中共川东临委委员、下川东地工委副书记)在下川东领导农民暴动失败，不幸牺牲。她忍住悲痛，要求地下组织将她再派到下川东，继续从事农村暴动的准备工作，不料于端午节后因上级领导叛变出卖而被捕。正忙于打内战的国民党当局为了防止后方农村暴动，急于从她口中得到有关地下组织情况，对她施用了酷刑，但她任凭拷打折磨，坚不吐实，关押至1949年11月14日被杀害。<br />
<br />
后来回忆录的作者们在创作小说《红岩》时又以她为原型塑造了仍旧称为“江姐”的“江雪琴”，实际上江雪琴的革命经历和主要事迹均与江竹筠相同(电影演员于蓝在扮演电影《烈火中永生》中的江姐时，就专门请当年江姐的战友刘德彬详细介绍了真实的江姐的各方面情况)。因此，一般人仍然把小说中以及后来根据小说改编的电影《烈火中永生》、歌剧《江姐》等文艺作品中的江姐都看成是历史上真实的江姐。<br />
<br />
<br />
<br />
风华正茂时的江姐<br />
<br />
虽然江姐在中共党内地位并不高，只是基层党组织(川东临委和下川东地工委)的联络员，与她同时代献身革命、同样壮烈牺牲的革命烈士何止成千上万，但她却为广大公众所熟知，江竹筠的名字和事迹还同许多革命领袖人物一起被载入了《中国共产党历史大辞典》的人物辞条中。在2003年重庆市评选“重庆十大历史文化名人”时，还有不少人要求选江姐……这不能不归功于回忆录《在烈火中永生》和小说《红岩》所引起的轰动效应。<br />
<br />
在人们所熟知的江姐的动人事迹中，有两个最让人印象深刻、过目难忘的情节，一个是她亲眼看到了被国民党当局杀害后悬挂示众的她丈夫彭咏梧(小说等文艺作品中叫彭松涛)的人头，另一个是她被捕后遭受了以竹签子“钉手指”的酷刑。<br />
<br />
遗憾的是，这两个震动了亿万公众的情节，恰恰都是虚构的。<br />
<br />
虚构这两个情节，并非是在作者们创作小说时，而是在他们写革命回忆录――报告文学时，甚至在此前为青少年作烈土事迹宣传报告时就开始了。后来刘德彬在一份回忆材料中坦诚写道:“事实上，烈士的一些英雄事迹也是被夸大了的。如江姐受刑本来是(夹)竹筷子，把它改成了(钉)竹签子；全文背诵《新民主主义论》，实际上是提纲和要点；江竹筠并未见到她丈夫的人头，而把她说成见到了。”1964年《在烈火中永生》再版时，因江姐看到丈夫人头的情节编造痕迹太重，被刘德彬提议删掉了。但江姐遭受竹签子“钉手指”酷刑的情节，仍然保留了下来，长期留传，影响深广。<br />
<br />
我们看看历史上对江姐事迹的介绍中，对她受刑的介绍经过了怎样的演变。<br />
<br />
1949 年11月30日，人民解放军接管重庆。在国民党当局溃逃前夕，对渣滓洞、白公馆等地关押的政治犯实行了血腥大屠杀。12月初，分别在11月27日大屠杀之夜从渣滓洞、白公馆脱险逃生的刘德彬、罗广斌等到“脱险同志联络处”报到集中。随即被安排到“重庆市各界追悼杨虎城将军暨被难烈士追悼会”组织部协助工作，参加整理烈士传略，提供给烈士资格审查委员会评审烈士参考。他们集中了萧中鼎、傅伯雍、盛国玉、孙重、任可风、杜文博、郭德贤、曾紫霞等脱险同志一起讨论，提供烈土生平及狱中表现等资料，罗广斌与刘德彬、凌春波记录整理，由罗广斌一人做代表向评审会议介绍情况，听候咨询。<br />
<br />
1950年1月中旬，“重庆市各界追悼杨虎城将军暨被难烈士追悼会”召开后，罗广斌、刘德彬等将有关材料整理成大会特刊《如此中美特种技术合作所――蒋美特务重庆大屠杀之血录》，印行3000册，分送有关单位和烈士亲属。其中，“被难烈士事略”中的“江竹筠烈士”一则，对江姐被捕后的受刑是这样介绍的:<br />
<br />
特务们一点不放松她，戴重镣，坐老虎凳，吊鸭儿浮水，夹手指……极刑拷讯中，曾经昏死过三次……<br />
<br />
1950年6月21日出版的重庆《大众文艺》第一卷第三期发表了罗广斌、刘德彬、杨益言3人第一次合写的文章《“中美合作所”回忆片断:圣洁的血花――献给九十七个永生的共产党员》。其中对江姐受刑是这样写的:<br />
<br />
特别是江竹筠同志，(特务)要想从她身上，找出一些关于她丈夫彭咏梧同志的关系，所以在魔窟的严刑拷讯下，她受尽了老虎凳、鸭儿浮水、夹手指、电刑、钉重镣……各种各样的酷刑……<br />
<br />
可见，在早期的介绍中，江姐所受的酷刑并没有什么竹签子“钉手指”，而只有一个“夹手指”。这个“夹手指”，即刘德彬后来所说的“夹竹筷子”，是通俗的说法，其实它就是古已有之的一种酷刑，名叫“拶”。<br />
<br />
拶，是旧时酷刑的一种，以绳穿5根小木棍(比吃饭用的筷子略粗)为刑具，名叫“拶子”或“拶”。行刑时，将受刑者手指分别套人木棍之间，用力紧收，叫“拶指”，简称“拶”。在明代凌鞒跛著话本小说《二刻拍案惊奇》卷十二《硬勘案大儒争闲气甘受刑侠女著芳名》中，就记载了南宋名妓严蕊遭诬陷后被绍兴太守严刑逼供:太守“就用严刑拷她，讨拶来拶指，严蕊十指纤细，掌骨嫩白……”京剧《窦娥冤》中，窦娥也受过此酷刑，她的唱词中就有“不招认实难受无情拶棒”。可知此刑早在元代就常用了，而且看来多半用于对女性逼供时。在早期介绍江姐事迹的文章中，如实写了她受到的是“夹手指”即“拶指”的酷刑。但是，不久之后，这个酷刑被改成了用竹签子“钉手指”。<br />
<br />
1957年2月19日出版的《重庆团讯》当年第3期发表的罗广斌、刘德彬、杨益言《江竹筠》(连载之一，编者按称选自即将完稿的《锢禁的世界》)，其中描写江姐受刑的情况是:<br />
<br />
绳子绑着她的双手，一根竹签子从她的指尖钉了进去……竹签插进指甲，手指抖动了一下……一根竹签钉进去，碰在指骨上，就裂成了无数根竹丝，从手背、手心穿了出来……<br />
<br />
1959年1月10日出版的《红领巾》半月刊1959年第一期发表了罗广斌、刘德彬、杨益言(不屈的心／在人间地狱――“中美合作所”》，其中写道:<br />
<br />
刽子手们把女共产党员江竹筠同志双手绑在柱子上，一根根竹签子，从她的手指尖钉进去，裂成无数根竹丝，从手背、手心穿出来……<br />
<br />
1959年2月中国青年出版社出版的罗广斌、刘德彬、杨益言合著革命回忆录《在烈火中永生》中，对江姐受刑也是这样描写的:<br />
<br />
一根根的竹签子，从她的手指尖钉进去，竹签钉进指甲以后，碰在指骨上，裂成了无数根竹丝，从手背、手心穿出来……钉进一根竹签，江姐就昏过去一次，接着就听见一次泼冷水的声音。泼醒过来，就又钉……<br />
<br />
以后在小说《红岩》中，江姐也是受的这种竹签子“钉手指”的酷刑。在根据小说改编的电影《烈火中永生》、歌剧《江姐》等文艺作品中，自然也都是一样。歌剧《江姐》第六场中，特务头子沈养斋在下令对江姐用刑时狂叫着:“把她的十个手指，给我一根一根地钉上竹签!”<br />
<br />
这样的细节，的确给人印象太强烈、太刺激了。因此许多人一提到江姐，就会想到竹签子“钉手指”。<br />
<br />
今天来回顾这段历史，可能有人会提出疑问:罗广斌他们怎么可以在宣传烈士事迹时不实事求是?这个问题，其实不应苛责罗广斌等人。因为在当年，他们既不是作家，也不是历史学家，都只是年轻的共青团干部。那时他们的职责和心愿，只是为了教育青少年，为了千方百计启发青少年的“阶级觉悟”和“反美”情绪而进行政治性的宣传鼓动，他们不是作严肃的历史研究，更不懂什么“学术规范”，加上当时强调的是“政治第一”，文艺、教育等等都要“为无产阶级政治服务”。在那样的大气候中，他们难逃历史的局限――请想一想，有的深通历史、深请学术规范的专家学者还有意剪裁、虚构历史来迎合政治需要，更何况他们这样的“激情燃烧” 的共青团干部?因此，他们在宣传讲演中没有拘泥于历史细节的真实，想的只是为青少年讲“革命故事”，而且要尽可能讲得生动感人。罗广斌在西南联大附中读书时就是有名的“故事大王”，讲起来更善于作艺术性的加工、渲染。这样，他们在讲演中就陆续加进了一些虚构的、夸张的内容，以后又写到了作品里。<br />
<br />
其实，真实写出江姐当年受过的酷刑不是“钉竹签子”而是“拶指”，也并不会就贬低她的英雄形象。在这里，值得探究的是，为什么当年在“教育(或文艺)为无产阶级政治服务”的口号下，为了“革命的需要”，对历史真相竟可以随意进行修改甚至虚构?如果沿着这条路走下去，将会走向何方?我们已经看到，后来有人把 20年代安源工人歌颂领导他们罢工的李隆郅(李立三)的民歌改成了歌颂毛润之；把率南昌起义部队上井冈山与毛泽东会师的负责人朱德改成了林彪；甚至把井冈山时期的珍贵文物红军布告上军长朱德和政治部主任陈毅的名字用香火烤掉，造成人为的“破损”，只留下党代表毛一人的名字……把这些教训联系起来，不是很值得我们反思吗?<br />
<br />
历史，毕竟不应虚构――不管是以什么名义。 听说今天在歌乐山烈士陵园展览馆的讲解中，已 经不再讲江姐被竹签子“钉手指”了。这是实事求是原则的恢复，也是历史的进步<br />
