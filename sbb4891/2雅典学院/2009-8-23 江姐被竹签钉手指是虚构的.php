<br />
<br />
<br />
<br />
<br />
　　“烈士称号”风波骤起<br />
<br />
　　<br />
　　全国解放后，大难未死的原川东游击纵队政治部主任陈汉书于1950 年lO 月回到奉节，担任奉节县城关区指导员。他和彭咏梧是生死之交。当得知彭咏梧的头、身仍分两处葬于深山野林，以及孙元良逃跑时将从云阳抓到的18 壮士集体枪杀在奉节县城大水井下，尸体也是草草埋葬时，他心痛异常，决定向当时的奉节县委请示，要求重新埋葬彭咏梧和18 烈士。<br />
　　然而，由于过去国民党对川东游击纵队的极端仇视，开动一切宣传机器，大肆造谣、诽谤这支队伍是“土匪”，以致在建国后很长一段时间里，此流毒在部分地区人民群众的印象中仍未得到澄清；又由于解放初期忙于建立和巩固政权的工作，当地政府不仅没对川东游击纵队的性质和功绩作出结论，有些人也认为这是一支“流氓队伍”、“成分复杂”，把一些游击队员与“土匪”、‘恶霸”相提并论。接着而来的便是“减租退押”、“清匪反霸”和“三反”运动。在这些运动中，在“宗派主义者”和“左倾顽症患者”的指导下，原川东游击纵队指战员受到极不公正的待遇与处理：以“土匪”、“恶霸”、“反坏分子”等罪名判刑的有100 多人，其中被判处死刑的有40 多人，142 人被开除党籍，460 人被开除公职，制造出大量冤假错案。更叫人痛心疾首的是，原奉大巫支队司令员陈太侯，在1951 年“清匪反霸”中被人诬告为“土匪”、“反革命分子”，竟不由他申诉，便被判处死刑。陈太侯惯使双枪，枪法极准，是小说《红岩》 中双枪老太婆的原型人物之一。有关部门、人员知道他的厉害，先把他的枪卸下，再由人左右前后的架着，押回老家枪决，当众宜布的罪状是“土匪”、“恶霸一方”等等。川东游击纵队司令员后又兼任纵队政委的赵唯，在“反右”期间也被打成“大右派”，被监督劳动改造。<br />
　　在上述这种大的政治氛围下，陈汉书要求重新安葬彭咏梧和为其恢复烈士称号一事的结局就可想而知了。当时的奉节县委明确表示：“彭咏梧是不是烈士，还要经过调查。”重葬彭咏梧一事更无着落。<br />
　　后来，陈汉书调任奉节师范学校校长。他毫不气馁，怀着对川东游击纵队和彭咏梧的深清厚谊与责任感，继续申诉。1965 年，陈汉书等人找到奉节县竹园区区委书记陈祝南，请他作证人。陈祝南当年是川东游击纵队队员，曾亲眼目睹了彭咏梧牺牲的过程。县民政局派人与陈祝南和时任竹园区区长的沈凯等．人一起，赶到100公里外的黑沟淌，把彭咏梧的头骨、尸骨取回县城。然而，县委的答复仍是：“要经过调查，才能确立他是不是烈士。”不同意安葬。无奈，彭咏梧的头骨、尸骨只好暂时放置在县委收发室收发员陈瑞之的床下。后来，陈汉书等人将彭咏梧的头骨、尸骨转到奉节县城后一个名叫卧龙岗的乱坟场，用几块石头草草埋葬。<br />
　　1966 年“文化大革命”开始后，伴之而起的红卫兵运动席卷全国。红卫兵这个“文化大革命”时期的特殊产物，在对待彭咏梧一事上却做了件好事。奉节县的红卫兵押着当时的县委书记和几个县委常委进行批斗，逼着县委一班人批了几万元钱作为修建彭咏梧墓的经费；并当即行动，在奉节城广场北侧抢修了一座高大雄伟的水泥坟墓，把彭咏梧的头骨、尸骨从卧龙岗迁出，在此重新安葬。<br />
　　然而，好景并没持续多久。一次，江青在讲话中说：川东地下党多是“叛徒”、“特务”、“没有好人”。不久，陈汉书被批斗、游街示众，关牛棚达五六年，前后被整长达1O 余年，家中子女也受到牵连。与此同时，彭咏梧墓地也遭到厄运。一些农民在坟头摆放粪桶，乱堆脏物、杂物。更有甚者，有的单位还想抢占彭咏梧墓地作建房之用。<br />
　　1977 年，奉节县政府准备将彭咏梧烈士墓迁走。此时已退休的陈汉书考虑这样做不妥，便找到县政府陈述意见。县政府还是那句话：“彭咏梧是不是烈士，你们有什么证据？拿出材料来。”事后，陈汉书感到，要使彭咏梧墓长期保存下去，关键是要真正恢复彭咏梧烈士称号；而要恢复彭咏梧的烈士称号，关键在于要有有力的证据。<br />
<br />
<br />
　　<br />
　　<br />
　　万里跋涉寻找知情人<br />
　　<br />
　　<br />
　　1979 年，在自己的问题还没解决的情况下，陈汉书与妻子曹蕙碧一道，自费到外地为彭咏梧取证。他们先到了重庆，但事隔几十年，一时找不到要找的人。彭咏梧的原配夫人谭正伦也已逝世。无意间，陈汉书得知彭咏梧与谭正伦所生之子彭炳忠在四川大学的消息，遂当即赶往成都。<br />
　　彭炳忠当时是四川大学一个系的党支部书记。他听了陈汉书的介绍，万分感动，当即取出了毛泽东1950 年给彭咏梧、江竹筠签发的烈士证书。陈汉书如获至宝，吩咐彭炳忠将烈士证书给他各复印1 份，并写了份文字材料。紧接着，陈汉书急如星火的赶回奉节城。当他兴奋地拿着由毛泽东签发的烈士证书找到奉节县委时，得到的答复是：“还要有文字材料。”事情又拖了下去。<br />
　　正在陈汉书愁眉不展之时，当年的川东地下党员、解放后任《 万县日报》 副总编、1 957 年被打成“右派”的杜之祥，背着个挎包找到了他，说要收集素材搞创作。陈汉书大喜过望，二人商定联合行动，收集有关彭咏梧的材料。他们向当时的奉节县委书记张恢映、副书记钟建做了汇报。两位书记不仅表态支持，还把陈汉书到重庆、成都取证的费用予以全部报销。钟建又指示再拿出公费400 元，作为陈汉书、杜之祥外调的经费，并由县委开具了介绍信。1980 年，陈汉书、杜之祥开始了外调的行程，所到之处均受到热情接待。在重庆，他们取得不少宝贵材料。然后他们赶到成都，在四川省委组织部更换了介绍信，随即两人从成都坐火车赶到北京。在北京找到了谭佑民。谭佑民是四川云阳人，1935 年与赵唯等人发动组织了云阳第一次武装起义。起义失败后，谭佑民转移到山西，参加了受党领导的山西决死队。抗美援朝期间，谭佑民任中国人民志愿军二十军政委，回国后先后曾任《 解放军报》 总编辑、第四机械工业部第一副部长等职。谭佑民“文革”期间被整，当时还未落实政策，夫妇俩挤住在一间房子里。陈汉书、杜之祥随后去找彭云，不巧的是彭云去了美国。他们由彭云的爱人带路找到了文化部副部长仲秋原。仲秋原的爱人何理宜当年与江竹筠的感情很深，关系很好。后来，他们又找了与彭咏梧有联系的原南方局青委书记荣高棠……<br />
　　在北京奔走一个多月，二人先后找了50 多人，了解到许多有关江竹筠和彭咏梧当年的英勇事迹。<br />
　　随后，陈汉书、杜之祥乘火车赶往上海，在上海水产局找到了何文逵。1941 年彭咏梧到重庆后，正是通过何文逵的介绍才到孔祥熙创办的中央信托局当科员的。何文逵与该局负责人有同乡关系。南方局指示何文逵掩护彭咏梧。何文逵利用这层关系，也掩护了其他不少地下党员。解放后，何文逵在中共中央组织部工作，“文革”期间受迫害，被下放到上海市水产局任办公室主任… … <br />
　　这一趟下来，二人在全国各地奔走的路程累计万余里，先后找了150 多人为彭咏梧取证，获得了大量宝贵材料。<br />
　　回到奉节后，陈汉书、杜之祥根据采集到的大量资料，创作了一部长达20 多万字的长篇报告文学－?D 《 红梅青松傲霜雪》 。然而，当他们把这部用汗水和心血铸成的报告文学送到报社时，报社却不予采用。道理很简单也很明了：川东游击纵队司令员赵唯与奉大巫支队司令员陈太侯还未平反。后几经交涉，并通过奉节县委书记张恢映找到当时的万县地委负责人，《 万县日报》 才接受了此稿。报社后来以《 彭咏梧的故事》为题，连载了40 多期。这是《 万县日报》 有史以来登载得最长的一篇稿件。看到刊出后反映不错，陈汉书、杜之祥又将《 红梅青松傲霜雪》 送到重庆出版社请求出版，重庆出版社在正题后面加了个“川东游击纵队的故事”的副标题全文出版，第一次出版5 万多册，后再版2 万多册，在社会上引起了强烈反响。<br />
　　乌云遮日终有时，中国共产党十一届三中全会召开后，全国性的拨乱反正提上日程，大批冤假错案得以平反昭雪。在这一形势下，中共万县地委组织部先后抽出9 人组成调查组，从1984 年6 月至12 月对当年川东游击纵队的组织活动及解放后的处理情况进行了调查，于1985 年元月向万县地委呈送了关于川东游击纵队的调查报告。报告提出了9 条处理意见：正式承认川东游击纵队是中国共产党领导的一支革命武装力量；对川东游击队员在历次运动中遭受的冤假错案，一律平反纠正等。另外，报告对统战人士、被捕、战场上牺牲、负伤致残、无依无靠的游击队员等等，均提出了处理意见。 在这一过程中，赵唯与陈太侯分别得以平反，恢复了名誉。彭咏梧烈士称号事件也水到渠成地随之画上了句号。<br />
　　1987 年7 月30 日，万县地区行政公署公布彭咏梧烈士陵园为万县地区纪念建筑保护单位。1988 年，在彭咏梧牺牲40周年后，奉节县召开了隆重的纪念大会，号召全县人民继承和发扬彭咏梧、江竹筠烈士的遗志，掀起了一场学习彭咏梧烈士精神的热潮。．</div>

							
							
							
							
