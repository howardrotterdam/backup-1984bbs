<br />
　　　　　　　　　彭咏梧烈士称号事件<br />
<br />
<br />
　　　　　　　　　　　　　O 陈启兵<br />
<br />
<br />
　　一部《 红岩》 的出版发行，震撼了一代又一代的中国人。小说中江竹筠的形象可以说是家喻户晓，人人皆知，但不少人对江竹筠的爱人彭咏梧和他所领导的川东游击纵队则知之甚少。至于后来围绕彭咏梧烈士称号事件发生的极不愉快的事件，以及不少游击队员所遭遇的错误对待和处理，更是鲜为人知。尽管那些冤假错案在十一届三中全会后被平反纠正，但作为一段特殊的历史，仍值得世人深思。<br />
<br />
<br />
　　　　　彭咏梧的悲壮历程<br />
　　　　　<br />
<br />
　　彭咏梧，重庆市云阳县红狮乡人，原名彭庆邦，即小说《 红岩》 中江竹筠的爱人彭松涛。1931 年，彭咏梧考入云阳中学。初中毕业后，为谋生路，他报名投考武汉某技工学校并被录取。赴校那天，彭咏梧乘坐的船到了武汉后却不靠岸，一个国民党的军官出来宣布：“你们不是来武汉读书的学生，而是去江西打**的壮丁！谁要逃跑，按军法从事！' ’一群青年人才知受骗上当了。对国民党的内外政策强烈不满、对共产党已有一定认识的彭咏梧，更是火冒三丈。当天夜深人静的时候，他约了两个胆大的青年，在船工的帮助下跳江逃生? ，… <br />
　　1937 年9 月，彭咏梧考入四川省立万县师范专科学校。入校的第一学期，他就秘密加人了共产党的外围组织?D 中华民族解放先锋队。1938 年10 月，他正式加人了中国共产党，不久担任万县师范党的分支书记。1 939 年暑假，中共万县中心县委派彭咏梧到重庆红岩村学习了1 个月，接受周恩来的教育。彭咏梧返校后，国民党已对他有所觉察，万县中心县委通知彭咏梧撤离。1940 年秋，彭咏梧担任了中共云阳县委书记。1941 年秋，以周恩来为首的南方局调彭咏梧担任中共重庆市委主要领导职务。1943 年，党组织派江竹摘协助彭咏梧工作。两年后，经党组织批准，两人结为夫妻，后生下儿子彭云。1 947 年他负责编辑《 挺进报》 。同年秋，中共川东临时工作委员会成立，辖湖北的恩施、贵州的遵义和整个川东地区，相当于一个省委，彭咏梧是临委成员。<br />
　　解放战争时期，四川是国民党重要的粮源、兵源地。当国民党大举进犯解放区时，川东临委在下川东发动武装起义，开辟敌后第二战场，控扼三峡口，阻止他们将四川兵源、粮源运出三峡，有力地配合了全国的解放战争。<br />
　　1947 年秋天，彭咏梧向中共川东临委请缨，与江竹筠等人由重庆赶赴下川东，在云阳农坝炉塘坪和受中共上海局派遣返乡的赵唯组建了川东游击纵队。彭咏梧任政委，赵唯任司令员。接着，彭咏梧、江竹筠等又奔赴奉节，组建了奉大巫支队。彭咏梧兼支队政委，袍哥首领陈太侯任支队司令员。奉大巫支队先后与敌进行了大小10 余次战斗，在蒋介石视为心患的大西南防线区域烧起了革命烈火，震慑了国民政府。蒋介石急调大批正规部队“围剿”奉大巫支队。迫于形势，彭咏梧将部队转移至巫溪县的红池坝，利用那一带的险恶地势、无边老林，重开革命新区。<br />
　　1 948 年1 月16 日，奉大巫支队一大队的100 余人走到巫溪边境上的鞍子山黑沟淌时，遭到胡宗南部柴祥云团的伏击，彭咏梧不幸牺牲。敌人将彭咏梧的头颅砍下，先挑到奉节竹园镇游街示众，再挂到竹园坪小学操场边的杨槐树上。罗广斌、杨益言所著的《红岩》书中，将彭咏梧的头颅说成是挂在奉节城的依斗门上，是文学笔法。彭咏梧的头颅后被当地农民取下，埋到竹园坪的宝塔下；留下的身体，由黑沟淌山民用石头、泥土草草埋葬。<br />
