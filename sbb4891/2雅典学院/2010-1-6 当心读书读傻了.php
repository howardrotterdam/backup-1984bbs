																											<div id="postmessage_416969" class="t_msgfont">当心读书读傻了<br />
<br />
一<br />
读书，能提高品德和智慧，成为好人和智者，但也不一定。读书读坏了、读傻了的人，天下滔滔皆是。<br />
<br />
即使是好书、智慧书，有些人去读，也会读坏了、读傻了的。例如，很多人读老庄读坏了，成为不仁不义的人，轻则阴冷，重则阴险，权谋成家----权谋是一种智慧，无所谓好坏善恶，以之为善则善，以之为恶则恶，但在中国，凡权谋家，往往坏的多，好的少。<br />
<br />
这且不去说它，只说读书读傻了的。<br />
<br />
庄子名言：圣人不死，大盗不止。本来作为一个事实判断，不无道理，但很多人借来作为反儒甚至反道德的依据，认为只有反掉道德、灭掉圣贤，才能社会民主、天下太平，那就大错特错、傻不堪言：变成傻逼了。<br />
<br />
二<br />
在《享受生命，笑对一切----答村夫网友》后，有游客发言：<br />
<br />
天下本大同（百虑而一致，殊途而同归），故致大同者，道之贼也；知行本不二，故倡知行合一者，儒之犬也。“吾本楚狂人，风歌笑孔丘。”――况丘之狗也。哈哈……<br />
<br />
认识一个人说不容易很不容易，说简单则简单之至，一言以为智一言以为不智。凭上面“一言”可见，这又是一个读庄读傻了的。<br />
<br />
在大同时代、在人人都知行不二的时代致大同、倡知行合一，那叫多此一举。那种人不是糊涂愚昧就是别有用心，斥为“道之贼也儒之犬也”，可以（不过说“贼”和“犬”也严重了。笑柄而已。就象在西方社会不断完善民制度，正常，但如果有人在民主社会以民主志士自许，就沦成笑柄了。）<br />
<br />
然而，在乱世追求小康、在小康世追求大同（包括在专制社会追求民主），在知行分离早已成为普遍而严重的事实的时代倡导和实践知行合一，那是一种善行善举大德大道，那是一种理想主义、良知主义的闪耀。斥为贼与犬，岂非善恶颠倒黑白混淆？<br />
<br />
可见，即使是好书、智慧书，有些人去读，也会读傻了的。轻者成书呆子，重者成傻逼，可不慎与？可不慎与？<br />
<br />
三<br />
类似读书读傻了的人物，当今社会简直如山似海。想找到几个智力发育正常、可以正常切磋交流者，难乎哉？太难了。<br />
<br />
许多自由分子及反专（制）志士也不例外，一下笔就是滔滔傻话。有个叫一家村主的，旧诗写得不错，也反专制，又是著名的江湖反儒派，脑袋则十分的瓜，曾在枭文《李敖三流，鲁迅不入流》后留言：<br />
<br />
真不忍心看到东海一枭堕落到这个地步。唉！<br />
<br />
《李敖三流，鲁迅不入流》不一定十分正确（我自己当然认为十分正确），如认为思想有偏观点有误，可以批评，但是，要从中找出丝毫“堕落”的痕迹，只怕佛眼也无奈。<br />
<br />
这位一家村主居然从中“看到东海一枭堕落到这个地步”，不知长了一双什么眼，如此厉害。哈哈哈。东海忍不住学村主的话叹一声：真不忍心看到一家村主们厉害到这个地步。唉！<br />
<br />
对傻子讲道理，往往被旁观者当作傻子。所以，在傻子堆中大讲道理者，要么也是傻子，要么就要立志于启蒙教育事业，置自身利弊于度外、置冷笑污唾于度外----这也是一种傻呀。东海老人，大傻瓜也。</div>

							
							
							
							
