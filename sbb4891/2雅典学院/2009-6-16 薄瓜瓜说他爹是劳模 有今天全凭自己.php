薄瓜瓜心中的薄熙来是个劳模<br />
<br />
青年周末：听说你喜欢“红歌”，在姥爷的葬礼上还带头唱起来？<br />
<br />
　　薄瓜瓜： 我从小就喜欢姥姥唱的那些歌，最喜欢的一首是《英雄赞歌》，王成的原型就是当时我姥爷在15军树立的特级英雄孙占元，还有黄继光、邱少云，也是15军的英雄，沈阳的烈士陵园中还有姥爷为他们题写的墓碑，爸爸带我去过。在我姥爷火化的时候，在八宝山，我就提议大家唱了这首歌。<br />
<br />
　　青年周末：那时你16岁。当时应该是一个很悲痛的场面……<br />
<br />
　　薄瓜瓜：正因为悲痛所以要唱《英雄赞歌》，送他们走要有点激情壮志，哭也要哭出精神来。<br />
<br />
　　青年周末：姥爷、姥姥对你的影响好像更大？<br />
<br />
　　薄瓜瓜：他们对我影响很大，我是在他们和四姨身边长大的。爷爷对我影响也很深，每次见面，都跟我谈许多道理，给我写字画画，他还给我画过一个一笔画的大鸭子！在我的精神成长中，妈妈付出最多。跟爸爸在一起的时间有限，但质量很高。他在我心里好像是个劳模，记忆中他老是在忙。我9岁时给他写过一首小诗，爸爸现在还记得：“时晚民子遇，独院无父存。要知答明归，作真忘盼儿。”讲的就是傍晚放学，家长都来接孩子，而我却看不到父亲，他在外地。听说明天要回来，但他工作太认真，竟然忘了自己的儿子！我小时候写过很多诗，爸爸特别喜欢这一首。他不只会背古诗，还喜欢背我的新诗。（笑）</div>

							
							
							
							
