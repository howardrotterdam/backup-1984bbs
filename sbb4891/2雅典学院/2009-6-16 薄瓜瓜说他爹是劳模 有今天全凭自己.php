													<h2>瓜瓜：咱不和屁民一般见识</h2>
						
						
																											<div id="postmessage_127841" class="t_msgfont">薄熙来之子薄瓜瓜：没必要与社会误解较劲<br />
<br />
2009年5月9日，薄瓜瓜被评为首届英国“十大杰出华人青年”，获大本钟奖。这则消息从英国媒体传到国内后，引发一阵热议。议论中既有对薄瓜瓜为中国人争光的赞誉，也有从他家庭背景引发的想象。一位年仅21岁的大学生在海外获此殊荣，作为薄一波的孙子、薄熙来的儿子、就读于牛津的薄瓜瓜到底什么样？不少媒体都在联系采访他。沉默了一段时间后，薄瓜瓜终于同意在回到重庆后的第三天与记者做一次交流。<br />
<br />
　　寒暄中透着一种成熟<br />
<br />
　　5月21日下午，重庆某酒店大堂。薄瓜瓜一露面，记者就看到一张阳光笑脸，隐隐透着稚气。他伸出手来和记者握手，询问记者能在重庆待多久后，便建议记者“在重庆好好玩几天”，寒暄中透着一种成熟。他的举止让记者有些意外，似乎和想象中的“高干子弟”有些不同。<br />
<br />
　　面对媒体，薄瓜瓜希望袒露的是真实独立的自我，他从小特殊的奋斗经历和家庭教育，可以让人们看到一个与一般猜测完全不同的薄瓜瓜。那才是原本的他。<br />
<br />
　　当选“十杰”，让不少人关注和议论薄瓜瓜。他不止一次向记者强调，自己还只是学生，最主要的任务还是学习，只有学有所成，将来才能更好地为国家和人民作贡献。或许，薄瓜瓜从小学到大学的求学经历，正是我们了解“学生薄瓜瓜”最重要也是最基础的一步。<br />
<br />
　　以自己的努力支持自己的学业<br />
<br />
　　记者：到国外上学是你最先提出来的？那时你才11岁，而且当时你就读的景山学校可是重点学校。<br />
<br />
　　薄瓜瓜： 对，但11岁也是少年了！景山学校的整体水平虽然很高，但对我压力不大，放学以后就没事了，心里感到有点“空”，不满足。爷爷说过：“花盆难养万年松，有志气的孩子要敢于闯一闯。”我想，自己为什么不能离开这个环境到外面闯一闯？邓爷爷小小年纪就到法国学习，眼界才那么开阔。我的想法得到姥爷、姥姥和四姨的支持，他们鼓励我“好男儿志在四方”。当时到哪儿去八字还没一撇，就不管三七二十一先从景山退了学在家自学。<br />
<br />
　　记者：为什么选择英国呢？哈罗公学是怎样录取你的？据说你是哈罗公学500年历史上第一个中国学生。<br />
<br />
　　薄瓜瓜：因为家里人分析，英国的英语最标准，公立中学的信誉在全世界也很高，所以就选了英国。<br />
<br />
　　可是一联系就发现很困难，听说英国人刚出生就得报名，还要在指定的预备学校考察好多年，最后参加全英统考，过了关才能入学。而我当时连英文也不懂。<br />
<br />
　　在希望不大的情况下，妈妈带着我，开始在英国帮我补习英文。那时比较苦，吃、住、行样样成问题，没有任何人帮忙，几天就要换个地方住。记得有一次，妈妈领我去一个新住处，又小又暗，条件特别差，我一进屋就说：“这不是贫民窟嘛！”妈妈一句话也没说。后来我对自己的态度特别后悔。<br />
<br />
　　不过我学得比较快，给了大家信心，所以他们还是鼓励我报考哈罗。正好另一所公立名校给我做了一次很系统的智商测验，也准备给我一个名额。我被送到预备学校插班，也没有像其他新生那样被要求降级。后来哈罗公学同意我参加面试，之后就宣布给我一个附带条件的机会――参加当年的全英入学统考，并规定了成绩线。结果我的成绩除了拉丁文得B，其他6门都得了A。由于成绩不错，得到了首届“科山奖”和助学金。从16岁开始，每年我都是奖学金学生。我很自豪，我是以自己的努力支持了自己的学业，没有给妈妈爸爸增添负担。<br />
<br />
　　记者：在英国学校里生活怎么样？<br />
<br />
　　薄瓜瓜：我上的学校管理非常严格，周末不放假，连本地学生也见不到家人。开始每星期只有60便士的零花钱，等于七八元人民币。我全部用来买零食都吃不饱，经常觉得特别饿。（笑）<br />
<br />
　　记者：每星期只有60便士的零花钱是学校的规定？<br />
<br />
　　薄瓜瓜：对，很多人对英国的名校有误解。英国的学校也是五花八门，私立高级学校靠钱也能去，但像哈罗这类非常传统的学校，绝不给你舒适的生活。英国的传统观念看不起那些谈钱谈地位的人，不少人连“贵族”这个词都要回避。这种学校的生活条件也十分简陋，甚至连暖气都不开，就是要让你吃些苦，磨练你的心志。<br />
<br />
　　记得那时候，我和一些同学饿坏了，到晚上就把枕头套扯下来，悄悄跑到厨房，看到有什么吃的，比如苹果啦、吃剩的牛排啦，抓过来就往枕头套里扔，拎回来大家一块分享。<br />
<br />
　　得高分没有任何秘密<br />
<br />
　　记者：你在哈罗公学就是奖学金学生，资料上还记载着你毕业考试得了11个“A加星”，名列前茅。学习对你是不是一项重大挑战？<br />
<br />
　　薄瓜瓜：看用什么心态学了。我学习有两种，一是学校的课业和考试，二是自己要修炼的东西。学校课业倒没多难。英国从小学三年级就开始上化学、物理、生物，当初我都没学过。我狠劲儿用两周时间把这三本教材都背熟了，结果这方面一下占了上风，预备学校毕业时还得到全校唯一的自然科学奖。在哈罗宗教课一开始比较难，这也是中国学生没有的课程。但当理解了其中的知识架构，也就没有难点了。后来我的学习笔记还被当做范本在学校印发了。实际上，学习没有任何秘密，得11个“A加星”也只是敢于定下这个目标罢了，只要努力，就没有“弱项”。<br />
<br />
　　在牛津更刺激，特别是PPE（政、经、哲）的本科，公认是攻坚战，有些人甚至先上牛津的研究生，再考本科。光读那么多经典原著，时间就紧绷绷的。而在读书中又会挖出很多不同的理论和思想，把学校的课业和自己的修炼内容结合，确实是不小的挑战。<br />
<br />
　　记者：考试前会压力大到睡不着吗？<br />
<br />
　　薄瓜瓜：不是压力大到睡不着，而是经常为了突击学习没时间睡觉。加上自己还选修了一些额外的课程，比如天文学之类。我觉得考试很有意义，能让你在短期内吸收大量的知识，突飞猛进。<br />
<br />
　　记者：你是说很享受考试？<br />
<br />
　　薄瓜瓜：（笑）我的觉悟还没这么高。但我享受每次考试之后的那种“痛快感”，大脑迅速膨胀之后的收获让人很有成就感。<br />
<br />
　　记者：是怎么想到要出这本《还有不同》的书的？<br />
<br />
　　薄瓜瓜： 这本书的内容其实是我平时积累的构思。我平时也会和老师讨论写作，正巧这些东西被出版社看到了，他们有兴趣出。其实后来我写的几本更好一些，但都是自己打印给一部分人看的，没有出版发行。<br />
<br />
　　记者：你好像有很多时间，除了读书、写书，还办这么多活动？<br />
<br />
　　薄瓜瓜： 我把做的所有事情都当做学业的一部分，比如通过演讲和辩论来激活自己的思想，体会到更深刻的东西。想做的事，我都能找出时间，我只不过是笨鸟先飞，每天只睡四五个小时。<br />
<br />
　　记者：你在哈罗公学期间还报名参加了军训中的“攻击性淘汰赛”，相当于铁人三项，最后坚持下来的只有8名学生，据说你是其中唯一的东方人。参加这个比赛为了证明什么吗？<br />
<br />
　　薄瓜瓜：有人说，东方人体力不行，我不信，偏要试一试。既然参加了，就一定要做到最好、笑到最后。不只为了荣誉， 也希望通过这个机会检测一下自己的体能极限和意志力。<br />
<br />
　　我没必要和社会误解较劲<br />
<br />
　　记者：不管你将来做什么，或许你取得的每一步成功都会伴随着一些质疑，你自己有过这方面的预想吗？<br />
<br />
　　薄瓜瓜：我想没必要和社会误解去较劲。需要做的事很多，与其把心思放在为自己正名、消除猜疑上，不如把精力投放在更有益的事情上，多做一些建设性的工作。当然，大家能给我更多理解，我会更愉快，理解毕竟比误解要好。但在不被理解的情况下，我也愿意去承受误解。妈妈很早就提出过“享受误解”，人家因为重视你才会误解你，而误解往往是理解的初级阶段和准备阶段。有太多的事情别人不容易理解，你也没有时间去跟每一个人详谈。既然如此，不如积极面对，从误解中了解社会，理解这些误解产生的社会原因。再往大处想，当今世界也有很多对中国的“误解”，与其用心为自己解释，不如为自己的国家多做些说明。<br />
<br />
　　“我不担心，我又没做什么亏心事，既然人们对我有猜疑有好奇，那我就应当更透明，让人看清我到底什么样。我妈妈说，人要彻底就要透明。”<br />
<br />
　　妈妈在事业的巅峰期毅然放弃<br />
<br />
　　记者：在牛津你跟别人有什么不一样？<br />
<br />
　　薄瓜瓜：本来每一个人都不一样，每个人都是独特的，教育的目的也无非是根据不同的特质而因才施教。你所指的不同又是哪一方面的？（笑）<br />
<br />
　　记者：我说的是作为高干子弟的不同。<br />
<br />
　　薄瓜瓜：这个太没有什么感觉了，大家都一样，学校里很少关注谁是什么家庭背景，彼此都是用学生的心态来交往的。<br />
<br />
　　记者：你和父亲作为两代人，在观念上有没有什么不同？<br />
<br />
　　薄瓜瓜：我很多观点都受父亲的影响。他总对我说，“事在人为，功在不舍，勤能补拙，要想做出点事，就要比别人更加努力，比别人更能吃苦”。如果一定要找出我们的不同点，当然也有。比如他总是要求我在牛津埋头苦读，争当“读书冠军”，少去参加学生会的社会活动。其实在牛津，还真的不容你分心，不能不埋头读书，不止一个同学因为学习压力过大而自杀。<br />
<br />
　　记者：你的母亲谷开来律师也曾名声显赫，但这两年看到关于她的消息越来越少了。<br />
<br />
　　薄瓜瓜：爸爸常常和我议论妈妈，觉得她很伟大，有思想，有创意，做什么事都做得非常好。当年办律师事务所很成功，为了避嫌，她在事业的巅峰期毅然选择了放弃。爸爸说，其实这不是一个人的损失，而是一个事业的损失。他甚至说，如果当时他自己退下来，支持妈妈去做，她会做得更好！她退出以后，就像隐居一样，不参加任何社会活动，爸爸有活动让她参加她也不肯。我特别理解，她最不愿意被罩在爸爸的影子下，失掉自己。她现在整天读书做学问，研究“比较文化”。<br />
<br />
　　其实这种情况在家里不是第一例。我叔叔薄熙成早就激流勇退了，然后一直致力于教育事业，现在已帮助贫困地区近千个穷孩子上了大学，还自己编教材，每学期给他们讲课。我觉得他很了不起。<br />
<br />
　　那是联欢和表演<br />
<br />
　　绝对没有出格的东西<br />
<br />
　　记者： 现在网络上有人贴出你在牛津和外国女孩的合影，被人议论成“夜店照”，也有人觉得很正常，你对此作何评价？<br />
<br />
　　薄瓜瓜：谢谢你提出这个问题。这张照片是在牛津大学的一次化装舞会上拍的。牛津每个学院每两周都要举行一次化装舞会，每次舞会一两百人，多的时候三百多人，这是牛津大学的传统文化，已经有几百年的历史了。<br />
<br />
　　化装舞会的特点就是要求师生都以表演者的身份参与其中，大家都故意做出滑稽、搞笑的动作，这种方式能让同学关系和师生关系更融洽。在这种类似狂欢的集体活动中，平时很腼腆的同学也往往做出怪样，解脱平时巨大的学习压力。但不管大家多么兴奋，也只是联欢和表演，绝没有出格的东西，和黄色更不沾边。你说的这张照片，我和两个女生站在一起，只是对着镜头做了点怪样，比如我有意歪戴着领带。<br />
<br />
　　把这说成“夜店照”让我感觉好像是“深更半夜”在“黑店”里干什么见不得人的事。其实，如果把整个舞会场面都照出来，谁看到也不会往歪处想的。据青年周末</div>

							
							
							
							
