靠勤劳致富的种粮大户们，为了扩大生产规模会花钱雇佣一批人，也就是协助处理农场事务的雇工，有的给工钱，有的供吃住，也有拖欠工钱或被虐待的。这“种粮大户”在过去有一个恶名，叫“地主”，这名字一下子让祖祖辈辈奋斗累积的资本肮脏起来。<br />
<br />
某国人很同情弱者，因为在某国，被统治的弱者是大多数人的角色，同情弱者又多少离不开“仇富”这种怪病――很多时候，仇恨会被利用成为无敌的统治工具。某年某月，一个湖南的长着满口绿牙齿的土鳖从山沟子里爬了出来，通过几年苦心研究，终于找到了一条出人头地的捷径――煽动和利用仇恨。<br />
<br />
靠“资本来到世间，从头到脚，每个毛孔都滴着鲜血和肮脏的东西”此类精神控制类教义，他集结了一群失足青年和穷鬼，其中就有一名姓薄的。<br />
<br />
那些仇富的农民被他们私下煽动，他们将种粮大户们判为邪恶的源头，并且像纳粹德国对待犹太人一样进行了几场大清洗。这不但帮助绿牙党们收买了人心，并名正言顺抢走了大量累积资本，这资本让他们养活了自己八年多，这资本让他们改善了自己的生活，这资本让他们建设了策反根据地，这资本让他们更加坚信了斗地主的好处多……而这历史，后来被极力美化，例如那两部扬名于世的艺术作品《半夜鸡叫》和《闪闪的红星》被要求作为教化红色接班人必看影像。从那之后，雇工们开始为“集体”种地，绿牙党成功的用思想控制了雇工们，并再也不愁粮食问题了。<br />
<br />
雇工们的辛勤劳作和热情支持让绿牙党见到了1949年，绿牙党拥有更大的权力后开始更大的抢夺。<br />
<br />
企业家的资产被收缴，保命的一群人则主动上缴了，这部分人或被美其名曰“民族资本家”。在特定时期需要特定的模范人物――荣家在此次运动中表现积极出色，通过美化和包装后被树立成为资本家与绿牙党合作的典型案例。同时，公民正式的丧失了土地权。敛财夺地的完成，也就是传说中的“改造完成”。<br />
<br />
钱和地被抢走了，留给我们的又是什么呢？ 饥饿和一本写满口号的红皮书……如果你对待这些稍有消极或悲观，可能会被游街、暴殴、劳改、关押甚至处决。<br />
<br />
后来狂热减退，公民意识稍有觉醒，但很快，饥饿了三十多年的雇工们被一句“让一部分人富起来”给冲昏了头，然而这个美丽畅想的背后是绿牙党后代们在疯狂买卖他们老子抢来的土地、并且通过特殊权力垄断行业聚敛资本――虽然这一切看起来不再像绿牙党当年做的那样粗暴，同时一些人对这些行径会自我安慰的说“现在日子不是比过去好多了嘛”。然而，这一次又留给我们什么呢？在依靠权力垄断的行业下，我们再次沦为地主的雇工……即便这样，他们仍不忘了早年的出山之道――红皮书里的口号以短信形式发给我们、自给自足后要唱赞美他们的歌……<br />
<br />
雇工们把资本、土地和基本权利都交给了他们，成就了他们的特权，这时候他们的后代却真诚的告诉我们――他们拥有的这一切和他们的家庭背景没有任何关系。</div>

							
							
							
							
