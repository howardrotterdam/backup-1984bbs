<br />
美国教授咋这样评价毛主席？<br />
两江居士<br />
2010-7-31<br />
我在上网上时，偶然中读到一篇美国教授评价毛主席的谈话。他说：“毛应该算是中华民族历史中的一个重要焦点，已经深深嵌入了中国历史”；“目前中国对毛泽东的诋毁丑化已超越了底线”。<br />
读完这位美国教授的谈话后，我就在想，作为一位天天熏陶在“普世价值”下的美国人，咋这样看待非毛者呢？咋这样评价毛主席呢？<br />
为了真切地反映这位美国教授的观点，我原汁原味地将其谈话转帖于此。<br />
美国哈佛大学亚洲研究中心主任、著名中国问题专家托尼&#8226;赛奇教授认为，本着友好的态度，应该提醒一下目前中国对毛的肆意丑化已经超越了一个民族应有的理智界限，且不论错误与否，毛应该算是中华民族历史中的一个重要焦点，已经深深嵌入了中国历史。历史就是永恒、历史就是先辈。一个民族的长辈，在世界上没有任何一个国家象中国这样恶意谩骂。就拿全世界人人声讨的希特勒、日本天皇来说，你们见过几个德国人、见过几个日本人像中国人这样恶毒谩骂过他们？他们可以忍受外国人对自己先辈过错的指责，但是绝对不会容忍本民族的人侮辱自己的先辈。这是一个民族的尊严，一个人的气节。很简单如果一个德国人或者日本人在纽约街头恶意辱骂他们的祖先，他能得到的也仅仅是好奇或聊以解闷的目光，转而就会遭人不耻，因为他是在侮辱他自己的民族，也是在侮辱他自己，一个连自己的民族都不热爱、连自己的先辈都能出口肆意谩骂的人，谁会看得起呢？“家丑不可外扬”出自中国，全世界都认可的哲理，而你们中国人现在却在违背。这是在自毁民族啊，是会影响中国在国际上的形象的。中国人应当自省啦！ <br />
以我观察，中国现在有三种人在污蔑毛泽东：<br />
第一种人,是传统西方政客在进行“和平演变”的需要。“和平演变”首先就是要否定那个国家最有影响的人在群众心目中的形象和地位，以便接受他们的思想，过去在东欧是这样，在苏联是他们自己否定了自己。<br />
第二种人是中国一些先富起来的人。他们“害怕”毛泽东思想回暖，给他们既得利益带来威胁，他们要维持现状，甚至是要建立有利于他们的政治新秩序，就必然要否定毛泽东。<br />
第三种人是一些过去毛的“仇人”的后代――国民党时代的官僚和地主的遗老遗少们，因为他们的先辈在“毛的时代”失去了大量利益，所以怀恨在心，于是对毛极尽诬蔑之能事。<br />
托尼&#8226;赛奇教授认为:无论我们承认与否，在我们西方的自由世界中，任何一个不带“有色眼镜”的人都尤其尊重你们中国的毛，他是一个值得所有对手尊敬的伟人，他将他和他所有亲人都献给了这个民族的劳苦大众。无论是过去、现在或将来，他的精神和思想不光值得我们西方世界研究、学习和尊重，更应该值得你们中国人了解、尊重和学习，因为他是你们中国人屹立于世界民族前列的财富和基石。<br />
我很遗憾，在我们西方世界，将毛先生的精神和思想上升到一种战略的高度来进行研究和学习的时候，在你们国内却出现了大量忽略、诋毁甚至是造谣中伤毛先生的事情。在我们的世界里，觉得这简直是不可思议……否定“毛泽东”是当代中国困局的症结。<br />
其实，我们西方世界特别重视和珍重毛的思想和著作，特别是在我们中国研究学会和军界，毛的思想和著作是必修课。我想你们也听说过了，毛的军事思想和政治理论早已经是“西点军校”中的必修课，而且也是学界在研究亚洲问题时首先要研究的对象。毛的为人在我们这些人看来更是不可思议的，就连尼克松这些他曾经的对手也不得不感叹，毛近乎是一个神，而不是一个人。世界上还有谁见过将自己那么多的亲人的生命都献给了这个国家和民族的解放事业的领导人？难道这样的人不应该值得所有的人珍重吗？如果还有谁也为这个国家做出了这么多的牺牲，才有资格来批评这个伟人……<br />
对于毛在历史上的错误，用你们传统辩证观点来看，这也是正常的，这个错误不应该归究于他个人，这应该是那个时代的错误，是那个时代所有人都在犯的同一个错误，只不过有人习惯性将那个时代归纳为毛的时代，自然就将错误全部归纳到毛的身上罢了。如果将那个时代所有的人犯的错误都归究到一个古稀老人的身上，这既会让人觉得可笑，也明显不公平（It’s　unfair!　It’s　unfair…他说到这里特别激动，连续说了好几次）……<br />
有一样事情我至今也没有想明白，在你们中国弃之不用的东西，在我们这儿却成了宝贝，我们用来研究、学习和改进后，反过来却用你们创造的东西变成了战胜你们的武器……<br />
历史上难道不是这样吗？<br />
后来，托尼&#8226;赛奇在谈到毛泽东思想与中国当前政治困局及未来时，指出：中国当前面临的最大问题是贫富不均造成严重的两极分化，而且这种两极分化伴随着权力私有化，中国的社会矛盾已经是越来越突出，毛的思想重新被更多的普通群众重视起来。如果一味地否认或者清除毛在中国思想界的影响，只会给中国带来灾难性的后果，甚至可能造成分裂！中国当前严重的腐败问题，既有制度上的缺失，更有思想上的混乱。要知道，在中国这个传统的特别注意道德修养的“礼义之邦”，政治上的腐败，很大一部分原因是制度上的缺失，但思想上的混乱，也是一个不可忽略的重要因素；而导致思想混乱的真正原因，就是新的思想并不为人认可，而毛的思想却被人为地极力淡化，这就是造成中国思想混乱的真正原因。<br />
最后托尼&#8226;赛奇呵呵一笑，说，这也正常，天下谁还没有一两个敌人，特别是政治家，尤其是你们中国的政治家……不过你们这些青年人面对这些谣言的时候，还是要有自己的判断，不要成为历史和世界的笑话！<br />
大家读完这位美国教授的谈话后，会有什么感觉呢？我以为，托尼&#8226;赛奇的认识是非常深刻的，头脑是非常清醒的，提出的观点切合中国时弊；特别是他对造成中国思想混乱原因的分析，更是十分睿智。完全可以这样说，没有对中国历史的深刻理解，没有对中国现实的深刻观察，没有对中国未来的深切担忧，是不可能得出如此结论的。更加难能可贵的是，托尼&#8226;赛奇不是单纯、孤立地就事论事，而是把它放在国际大背景下进行理性分析。<br />
