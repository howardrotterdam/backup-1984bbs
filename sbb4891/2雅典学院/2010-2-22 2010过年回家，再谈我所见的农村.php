													<h2>2010过年回家，再谈我所见的农村</h2>
						
						
																											<div id="postmessage_487954" class="t_msgfont">『天涯杂谈』 2010过年回家，再谈我所见的农村<br />
<a href="http://www.tianya.cn/publicforum/content/free/1/1813847.shtml" target="_blank">http://www.tianya.cn/publicforum/content/free/1/1813847.shtml</a><br />
 文：落月先生<br />
<br />
虽然，我曾经在许多时候，在许多人面前，表达过我对家乡的热爱。捉过鱼的小河，翠绿的麦田，甚而是田野里的一座座坟，都曾是我无数次情感停留的所在。但是，我也知道，这些都只是回忆，已再也无处可以去找寻。河水已经干涸，河道里满是花花绿绿的塑料垃圾袋，麦田虽然还在，但一条正在修建的高速公里横穿而过，像一道伤口，将那片田野硬生生撕裂开了一般，至于坟头，也早已经失去了对死亡的尊重和对祖先的纪念，低矮的土丘，像一个个干瘪的高粱馒头。甚而在过年的时候，也很少见到坟头前有青烟散起，人们或许是过的太忙碌和紧张，而忘记了过年前要去坟头烧两把纸钱。<br />
当对家乡的情感成为一种凭吊的时候，或许家乡在远去，我也在远去。每年一次的返乡，像是在履行一种责任和义务，回去看一看逐年老去的父母。也像是去祭奠，祭奠逐渐病去或已经死去了的村庄。<br />
　　我无意执迷于田园牧歌式的乡村，我也知道工业化的席卷，或许是现代化的必然之途，也是目前唯一的“解救之道”，我亦然知道，中国的乡村之沉重，农民之艰辛，非是一朝一夕就可治愈。但是，我更知道，甚而是确信，我所见的农村，并不是一个健康的、正确的农村景象。<br />
　　去年回家，曾写过一篇散记，有些问题也就不再重提了。如见：<br />
　　<a href="http://www.tianya.cn/publicforum/content/free/1/1497960.shtml" target="_blank">http://www.tianya.cn/publicforum/content/free/1/1497960.shtml</a><br />
<br />
一、观念正在改变。<br />
　　或许农民观念的改变可以视为是农村一些问题的症结所在。挣钱，捞钱，能挣多少钱，在农村已经成为衡量一切的价值标准，成为人们日常生活中的主要谈话内容所在。这也可以视为市场经济对农村造成的最大的影响。<br />
　　在家几天，听父辈乡邻们之间的谈话，大多是关于是谁在外挣了多少钱，谁家挣钱在县城里买了房子。无论你人品怎样，即使你之前是个流氓泼皮无赖，倘若你能挣到钱，也是会被尊重和肯定的。你出手阔气，抽的烟好，穿的衣服好，家里房子盖得好，就可以将其他的一切都给遮掩过去，这真可谓是一钱遮百丑了。<br />
　　回家，常听父亲说到村上谁时有这样的感叹：“唉，无论怎样，人家现在能捞到钱哦”。古人云，一文钱憋死英雄汉，现在则是，无论是英雄还是狗熊抑或是奸诈小人，有钱才是真好汉。<br />
　　我跟父亲说，人活着不能单单为为了钱考虑。父亲说，人活着不为了钱，还能为了啥呢？<br />
　　说到这，不由想起了二十年前，所谓的“黑猫白猫论”，这的确是一件吊诡的事情，倘若以能否抓住老鼠为衡量是否是好猫的唯一标准，这个标准，或许有其特定的时代合理性，但并不应该成为放之四海而皆准的普世真理。长此以往，中国将来不愁没有本土宗教，“拜金钱教”或许有可能。试想一番，当全民都陷入一种对金钱的狂热崇拜之中时，这样的一个民族，或许是可怕的，也是危险的。<br />
　　一切向钱看，无论其他。诸如目前在家乡特流行的观点：谁家让孩子上大学，谁家倒霉。花了钱，出来还没啥大作用。又不是铁饭碗，现在到处都是大学生。还不如及早出来打工，打工一个月也能挣好几千。想到之前《南方周末》报道的教育致贫现象，以及“蚁族”一词的流行。或许可以诠释这一切。<br />
　　而观念的改变，恰恰是最让人触目惊心的，由此而形成一种社会风气。固然大学教育目前有很多的弊端，乃为大家所诟病。但试想一番，倘若将来受高等教育的群体，主要是城市的孩子。而农村的孩子只能高中毕业就以出卖劳动力的身份出去打工，或是受过一两年技术培训就被输入进现代化的生产线上，那么，长期如是。农民只能是一代打工又一代打工，这与以前的那种一代放羊，生了孩子养大了还是放羊又有何异？这从目前大学生中城市与农村出身的孩子的比例就可见一端倪，尤其是一些名牌高校更甚。<br />
　　当然，我并非是歧视体力劳动者和打工者。但我想，一切不应该这样，不应该就由农民和农民的孩子来从事这些工作。<br />
　　但是，他们不去城市打工，不拿青春去出卖劳动力又将如何去呢？谁会天真地以为，取消了农业税，对于农民来说就是一个天大的福音？只不过是象征意义大于实际意义罢了。<br />
<br />
二、观念正在改变（之二）<br />
　　观念的改变，有些的确让人欣慰。诸如计划生育问题，起码在我们那里，年轻的一代，已经有大多可以接受只有一个孩子，认为男孩女孩一个样。当然，在他们谈及的时候，更多是将原因归结于养不起太多的孩子。<br />
　　观念的改变，还表现为农村人也正在抛弃农村，甚而是厌恶农村。或许这是城乡之间的差距在这二十余年来逐渐被拉大的结果，或许是持续二十年的打工潮，使得农民对于城市的生活越来越向往，无论是年轻的一代，还是五六十岁上了年纪的人。村庄更像是一个驿站，每年只有夏收、秋收和过年的时候，人们才会回去几天。等忙完了之后，又迅速撤回到了城市之中，只剩下孩子和老人，有人甚而将孩子也带走了。<br />
　　在美国的华裔，有一个词叫“香蕉人”，而农村的这些年轻一代，在进入了城市之后，也迅速地把自己包装成一个城市人，甚而是更像一个城市人。如是的行为，是在掩饰自己的农民的身份，还是隐藏着一种巨大的渴望？<br />
　　对城市的渴望，也带来了我们县城的城市化的巨大推进，尤其是房地产的兴起。而这些房子有很大一部分是卖给了农村的那些打工富裕者。由此而形成了他们长年在外打工，住在县城。家里的土地交给六七十岁的父母打理。等收获的时候，才回去一趟，将粮食收了之后就迅速卖掉。或是把名下的那份土地承包给别人。<br />
　　说到打工，再多言几句。回去之后，父母有时抱怨，说不如让我当初不读书，说我现在还不如村上那些比我年轻的，初中毕业就出去打工的（我要为自己的住宿吃饭自己开支，而那些在外打工的，基本都是包食宿的，生活成本相对很低。我父亲冬天的时候就跟一个包工队干过活，每天三顿只有五元钱的伙食费）面对父母或真或假的失望，我只好忍不住地说上一声，说他们的下一代或许还是出去打工，而我的下一代起码不会再打工。<br />
　　做工，到城市里去。城市并不为他们的养老，医疗负责。想到之前读的一本书《英国工人阶级的兴起》，比照而言，不为悲观地说，中国的打工者，基本上无法形成一个新的工人阶级，因为他们没有文化凝聚，因为他们处于一种畸形的“商品经济”之中。当然有新词曰“打工阶级”。至于打工者的明天，打工者的下一代问题，热议了好些年了，仍然基本如是。<br />
　　甚而为了让自己被剥削，还要自己支付高昂的成本，学缝纫，学纺织，学焊接。这些年，我们县城里多了好多这种职业技术学校，或挂着某职业技术学校某县分校的牌子。县电视台可以牛叉到在我家有线电视的所有频道下方，都打上本县的广告。诸如某学校招生，某厂招工、某楼盘开售，某药品优惠等等。<br />
　　其实，有些时候想一想，我也如是罢了，和他们一样，只不过是城市里的民工。<br />
<br />
三，黄赌毒<br />
　　黄，正所谓“本不是爱风尘，却被风尘误”。无论是我所在的上海，还是内地的省城抑或仅仅是一个偏僻的县城。每个夜晚的粉红色灯光下，坐着多少年轻漂亮的女子在那里等待着客人？这些女子来自哪里？我想，大部分是内地的农村。<br />
　　她们也只是打工者罢了，到城市里来赚钱。我们村就有一户人家就如是。其孩子和男人都在家里，她常年在外，基本上不回去，家里今年冬天新盖起了楼房。村里人私底下都晓得她是在外做什么的。只有一些老人说到的时候，还会表示出一种不屑，其他人也大多默许了。常言，笑贫不笑娼。的确如是。<br />
　　我对于她们更多的是同情，或夹杂着一些哀怜。她们只不过在出卖她们最有价值的商品――年轻的身体罢了。这已并非是一个道德问题，而是一个经济问题，没有一个女人天生愿意做妓女的。<br />
　　说到这，还要提一个乡村卖淫问题。真事暂且隐去不提，则是一些四十岁左右，业已成家生子的女子，稍为一些姿色。在乡村之间卖淫。这样的家庭，大多男人常年在外打工。“谁去都行，五十块一次。不然打电话叫上门也成。”这是我从一群男人的谈话中所听到的。<br />
　　赌，则是一种常态化现象。尤其是过年这几天。在外打工的年轻人，有了钱，赌得都很厉害。村上一个小青年，在我们那一片的十几个村里的商店里都安放了一个老虎机。每天开着车子去倒硬币。我想，留在农村的那些上小学初中的孩子，应当是玩老虎机的主要顾客吧。<br />
　　毒，我原本以为离我们那里的农村很远。这次回家才听说这样的事，也是我们村的。免不了提一下，我们村，好像是一个奇妙的景观世界，浓缩了一切，像一个缩影，成为三十余年来中国农村巨大变化的表征。<br />
　　事情是这样的，村里有一户人家，有两个儿子。小儿子辍学后，外出打工，在浙江某地，短短四个月，带回了十七万。至于这钱的来路，其父母不仅不去过问，反而责骂其大儿子无能，最后让大儿子也跟着小儿子去了，钱是被其父母拿来盖了两层小楼，以备将来给他们兄弟俩娶媳妇用（目前，在我们那里，农村的男孩子娶亲，基本上是要盖两层新楼的，这与城市里买房倒是如出一辙。）结果后来事发，两个孩子目前都无踪影。楼房还没盖好，只是毛坯。父母也外出，年没在家过，有人说是去寻儿子了，有人说是出去躲着了。回去听父亲说，原来那小儿子在外是跟人贩毒的。至于其两个儿子的下落，有人说被逮捕了，有人说是被同行给“做掉了”，具体无从知晓。<br />
　　父亲在说到这个事情时，叹气说，打工就打工，怎么能不走正道呢，虽然来钱快，但犯法的事情怎么可以做呢。母亲说，那家人也真是的，也不想想，小儿子带回来的那些钱是怎么来的，不仅不过问，怎么还可以把大儿子也赶去呢。<br />
　　至于吸毒，目前在我所看到的农村，还没听说这个事情。但我想，或许也会发生的。<br />
<br />
四、对权力的崇拜和对政治的冷漠<br />
　　说到对金钱的崇拜，不得不提农民对权力的崇拜。通俗而说，就是对当官的羡慕和崇拜。<br />
　　“当官的哪有不腐败的？还是当官好啊”村人如是说。“乖乖，你看人家胡锦涛，家中的祖坟一定冒青烟了。”村人亦如是说。“当今的天下是谁的，胡锦涛的。”村人依然用朴素的皇权意识来看待政治。<br />
　　当官不仅意味着有钱，还意味着有势力，甚而可以仙及鸡犬。时常可以听闻有讲述某村上的某人在外当官，然后将全家都接走了，又帮谁谁安排了工作，又几个电话就摆平了某件事情。<br />
　　这样的观念，大多停留在四五十岁以上的村民那里，至于年轻的一代，由于长年迁徙式地往返于城市与乡村之间，似乎自主远离了政治。或许更是因为，他们自知自己无缘于政治参与而主动放弃。至于基层民主化建设，更是空谈。村庄中的权力还大多把持在一些旧有的“政治精英”手中。目前我们那里还没有所谓的大学生村官，不知道别处这样的一种措施，能否在基层社会中培育出民主的土壤和一批新的政治权贵出来，对此，我并不持乐观态度。<br />
　　但是无可否认的一个现象就是，中国之农村，正在出现一种“政治真空”的情形。国家权力的触角自集体化制度瓦解之后，便逐渐从农村退出。农村似乎不仅在经济上被城市被现代化的市场经济所俘虏，沦为附庸。在政治上，也在逐渐地被国家所抛弃。放肆地言一句，所谓的一些人大中的农民代表，更像是沦为了一种摆设性的景观装置。暂且不论特定的时代因素，陈永贵现象，只是一种历史中的风景。当然，或许在当时而言，其也是一种风景和陪衬。农民也将永远地成为被代表的大多数，政治上沉默无声的群体。<br />
　　韦伯曾批评德国边境的一些农民，不关心国家政治，是市侩。中国当下之农民，似乎也如是。当然，对此我无意地批评，或许他们是聪明地远离了政治，或许他们为了生活之艰辛，只需要对金钱的追逐。<br />
　　父亲一直对我没有入党的事情耿耿于怀，这次回家，又提到了这个事情，我没有置辩。政治的天花板距离头顶是那么的近，我又怎么能长袖善舞呢。<br />
　　最后说一起发生在我们县的杀人事件，某人的一个儿子在省内的某个市做一官职（据说好像是政法委副书记或其它），其依仗儿子的权势，俨然成为村中一霸。后村内一人愤然将其杀之，沉尸水塘，案破，其曰，我是为民除害。这则故事，在乡里传为奇谈。<br />
五、合作医疗与农村养老<br />
　　农村也有了合作医疗，这的确是个好事情。每人每年交一些几十块钱，在村卫生所看病可以报销30%。这次回家，却听闻好多人提到合作医疗的时候，却有怨言。诸如虚开药价，同样的药，在县城的药房买只需要两元钱，在村卫生所开出来却要三元钱。诸如即使是个小病，也先让你打几天吊针。<br />
　　年中在家喝酒的时候，还听说这样的事，村上的卫生所，每次开药是从来不出具电脑打印的明细账单的，而都是在每天的营业结束后的晚上，重新做一份新的明细账单，以被上报或检查之用，因为有些药是被纳入合作医疗的，有些药没有被纳入。至于农民最后用到的到底是什么药，是否享受了国家的福利，无从知晓。<br />
　　合作医疗的卫生所是自负盈亏的。医生的收入，据说是每人都有六七千元，而附近另一个村的卫生所因为交通便利，那里的医生的月收入，每月都有近万。而我们县城的房价是2000/平方米，县城高中的一个老师的月工资是不足2500.<br />
　　此外，由于农民长年迁徙在外，你不可能让一个农民工在外得了病而跑回去治疗。所以，村卫生所的主要病人都是一些上了年纪的老人，妇女和孩子。至于医疗条件，医生的水平则是另一层面问题。<br />
　　农村养老，老人的生活还是要靠儿女的赡养，靠自己名下的那或多或少的田地，而这交给儿女打理的土地收入，并不能足以承载他们的生活成本，以至于养老在农村逐渐成为一种沉重的负担，或迫使得老人的生活质量下降。我们那里，而更多的情况是，老人还是自主生活，除非是衣食完全不能自理，不然还都是一人或老夫妻两人住在一处小房子内，儿女每年给些粮食和零用钱。有些老人得了癌症一类的病，其本人和家人基本上是主动放弃治疗，然后等待死亡。甚而有些被病情折磨的老人，恳求自己的儿女用农药将自己药死。<br />
　　儿女赡养老人，的确是天经地义也无可推置，但我想，或许还会有更好吧。顺提一下，我父母常与我笑言，他们把我送出来读书，而他们将来则面临老了的时候无人赡养的局面。对此，我只能沉默，或许努力在这个城市挣些钱，买个房子，再存些钱，再祈求父母的身体可以一直健康。十几年后，将年迈的父母接出来在身边，才是唯一可以避免这种情况的办法。这固然是一条正确而艰辛的路，责无旁贷吧。<br />
　　从这一点，也足以可以说明为什么农民对孩子，尤其是供儿子读书的兴趣逐渐下降。不然，将来儿子不在身边，土地无力耕种。让年迈的他们蹒跚在乡间村里，谁来关心他们？<br />
<br />
六、直面还是回避？<br />
　　看美剧或美国电影的时候，常会有这样的一个镜头。一个白人在大街上多看了一个黑人一眼，然后那个黑人就说那个白人是带着种族主义的有色眼镜歧视他。或有时候往往是白人在回避谈论种族主义，而黑人自己却每每认为自己是种族主义的受害者。<br />
　　看了大家的回帖，如是而想到的，就像有人谩骂我写这些文字是在歧视和瞧不起农村，是带着一种所谓高傲的姿态去对农村指手画脚，是所谓的忘本。<br />
　　出外求学十余年来，我并没有任何时候掩饰过自己的出身农村的身份，甚而对这样的事实，带着一份骄傲。无论是农民，还是农村抑或是城市，还是所谓的城里人，在我这里并不具有价值上的高低。我只是在陈述农村的一些问题，并没有站在城市的位置去审视和打量农村，也并没有由此而肯定城市/城里人，而否定农村 /农民，这并不是一个二元对立的问题。我是在说农村的问题，并非是肯定城市的好，没有问题。而只是因为，我仍然是一个农村人，而不是一个城市人，因为在农村生活着我的父母，我的乡亲。<br />
　　写下这些文字，或只是因为内心的一抹挥之不去的情怀。外在的殖民或许并不可怕，内在的，自我的殖民，才是可怕的。不仅有经济和政治的殖民，还有文化的殖民。就像我回去的时候，常能听到一些在外打了几年工的二十岁左右的年轻人，向我抱怨农村的落后，农村没有路灯，农村没有大型商场，农村老一代人的思想太封建，于此同时，他们却表达了对城市的羡慕与渴望。于是，他们年纪轻轻就被吸引到城市中去了，但城市是否就是温情脉脉地给予了他们温暖？当劳动密集型的产业结构升级，他们年老了之后，他们又怎么办？<br />
　　而那些稍微上了一些年纪的人，在谈到这个的时候，只能将这一切归结于无可反抗的宿命――谁让咱生在农村呢？<br />
　　亦如某些学者所言，中国目前的城市与农村，是一个孽债，是“现代化的原罪”，固然，现代化发展是不容质疑的，但这是否就能够剥夺我们质疑和反思的权力，是否就应该回避一切？<br />
<br />
七、从农村问题说开去<br />
　　坦诚地而言，无论是去年的文字，还是今年的文字，对于我而言，都是一种痛苦，像是在撕裂自己的伤口。我更无意于将我的家乡以一种“揭丑”的方式，展现在诸位的面前。但每每回去之后，总觉得应该说些什么，正所谓的“意难平”吧，而文字，或许对我而言，仅仅提供了一个释放的出口。<br />
　　诚如众位所言，我之关心的问题，乃是中国之农村究竟该往何处去。如果我之父辈，我之兄弟注定了要作为中国之发展的牺牲品，沦为历史的一个潜在的无声的注脚，那么，我不希望的是下一代还是重复着这样的命运。<br />
　　毕竟，回顾历史，我们存在过一种可能。作为一个后发的现代化国家，我们应当汲取教训。所谓的“先污染，后治理”的模式，是否就是唯一的正确之途？<br />
　　先行的革命者，在探索“中国向何处去”。于是而有了洋务运动，有了维新变法，有了辛亥革命，有了新中国之成立。那么，在当前，“中国之农村又该向何处去？”，这样一个庞大的群体性问题，不应该也不应当被忽视。<br />
　　而如何处理农村与农民的问题，本应该作为现代化发展的一个主旨性诉求。而不同的诉求，也会带来不一样的结果，而这个诉求，实质上却被忽视了。<br />
　　固然有每年的中央一号文件，但这更像是唱戏，农民仍是沦为了配角。取消了农业税，取消了义务教育的费用，有了低保，有了合作医疗。看上去光鲜无比，但实际上呢？花团锦簇的好一番热闹，羞煞我等农村人。<br />
　　十年了，依然如故，再十年后，仍会是如故。<br />
　　但无论怎样，这一切都将成为历史。至于历史又将以怎样的一种姿态来被后人评说，无人知晓。只是遗憾的是，农民/农村并不是书写自己历史的主体。他们只是被讲述，他们参与创造了历史，但这历史并不属于他们。他们真的很艰辛，也很可怜。<br />
　　我或许说的足够多了，也该沉默了。</div>

							
							
							
							
