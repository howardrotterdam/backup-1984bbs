													<h2>反政改势力势焰熏天，温家宝地位岌岌可危</h2>
						
						
																											<div id="postmessage_521346" class="t_msgfont">作者 : 曾节明,<br />
&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp;l表rg：3/15/2010<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;正当胡锦涛党务系统大树马列、高调批判“普世价值”、鼓足马力向全国 推行“六个为什么”1的极左洗脑工程之际，总理温家宝，再次大赞民主，与胡锦涛公开地唱反调。温家宝最近在与网民交流公开活动中说：“只有民主才不会政息 人亡”。三月九日，温家宝在作两会政府工作报告时说：““创造条件让人民批评政府、监督政府，同时充分发挥新闻舆论的监督作用，让权力在阳光下运行。”这 无疑是给胡锦涛一贯高唱的“政治正确”、媒体一律等党专制理念播送“不和谐”杂音。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;这已经是今年开年短短三个月中，温家宝第三次在重大问题上与胡锦涛唱反调：虎年春节，胡锦涛七年来力主上马的乘火车实名制，总算为铁道部试行，该实名制 的试行耗费巨资上亿元，名为打击“黄牛党”，实为压缩国民旅行自由，复辟极权控制；几乎在“实名制”试行的同时，温家宝的国务院系统却召开新闻发布会，与 会的铁道部高官，公开在会上唱衰“实名制”――铁道部政治部主任难得地说了真话：“一票难求”的问题根源不在“黄牛党”，而在铁路运力，“实名制”只会徒 然增添成本和麻烦云云。在这件事上，温家宝的国务院系统，简直是“跟总书记对着干”。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;事实上，温家宝与胡锦涛唱对台戏由来已久：<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;早在2008年汶川大地震中，胡、温之间的严重分歧就暴露出来：事前，温家宝力主发布地震警讯，胡 锦涛却拍板隐瞒；灾害发生后，温家宝力主第一时间开放救灾，胡锦涛却下令对外封锁灾区七十二小时；温家宝的救灾讲话务实动情，温家宝所到之处轻车从简、尽 可能不干扰救灾，胡锦涛的救灾讲话却处处党八股、篇篇假大空，胡锦涛的灾区视察，讲排场、重形式、前呼后拥、所到之处，甚至要救灾人员停下来听其训 话...这些都严重干扰了救灾。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;2008年九月，温家宝在美国联合国总部开会期间，充分肯定普世价值，明言自由民主属于全人类的成果，不应为资本主义国家所独有；温家宝更在接受CNN 记者采访时，面对当年自己站在赵紫阳身后的“六四”照片，下颌发颤地说：“我相信，在推动经济改革同时，我们需要推进政治改革。”。同一时间，胡锦涛却下 令封杀温家宝所有相关言论，并且通过李长春、刘云山、吴邦国、贾庆林等人前台站台，发起大规模的反“普世价值”批判运动，期间于2008年底，胡锦涛亲自 下令逮捕刘晓波，全面打压《零八宪章》签名运动，掀起反“普世价值”运动高潮，并随镇压零八宪章运动一到，贯穿了2009全年，这股颠狂地极权倒退意识形 态逆流，至今非但未见退潮，还随着“六个为什么”工程的推进而继续泛滥... ...<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;胡锦涛亲自发起的反“普世价值”运动，其调门之高、规模之大、反自由民主口气之强硬，实乃“改革开放”以来之罕见，比起“六四”屠杀后的“反自由化”批 判，其实有过之而无不及。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;早在2008年三月，十七大后权力获得巩固的胡锦涛，即迫不及待地弃用中共十五大提出的“忠于祖国、忠于人民、忠于法 律”政法工作口号，亲手抛出“党的利益至上、人民的利益至上、法律至上”（即“三个至上”）的极左倒退口号，公开鼓吹党权至上，排斥法治、民主；同年五月 四日，温家宝却在视察中国政法大学演讲时大谈民主法治，提出“法大于天”，温家宝与大学生的对话中，没有一个字提及“党的利益至上”，这就与胡锦涛形成了 鲜明对比。温家宝的讲话，事后即遭胡锦涛、李长春把持宣传系统压制，直到2009年12月24日宪法宣传日时，《新华社》才给以全文发表。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;胡、温之争还表现在经济上：2008年下半年，胡锦涛大力推动不触动土地公有制的“农村土地流转”，企图重新加强国家土地垄断、压缩土地承包制；而温家 宝却公开反对之。在胡锦涛的支持下，胡派干将汪洋利用经济危机，以“产业升级”为名，在广东大搞国进民退的“腾笼换鸟”，动用行政手段整垮大批民营企业， 美其名曰落实胡锦涛的“科学发展观”；温家宝则强烈反对“腾笼换鸟”，强调保护和鼓励民营企业发展。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;胡锦涛与温家宝之间的矛盾，一度激化到人身 丑化的程度：2009二月三日，访英期间的温家宝于剑桥大学发表演讲时，遭德国留学生扔鞋，胡锦涛控制中央媒体，居然一反森严的党国领导人报道规定，全程 播放温家宝遭扔鞋视频...胡锦涛存心丑化温家宝的用意十分明显。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;以上诸多事实充分表明：胡锦涛与温家宝分歧巨大，根本是格格不如的两种人。胡、温两人的重大差别，其实早已为赵紫阳所证实。赵紫阳在与宗凤鸣的谈话中 说：“他（胡锦涛）是在我们党正统意识形态所谓‘驯服工具’教育出来的一个青年干部”2，赵紫阳又说“温家宝的改革意识要好些，温是在改革开放潮流下发展 起来的...3”道明温家宝具备相当的改革意识。赵紫阳是胡、温当年的顶头上司，其评价的权威性不言而喻的。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;毛左派对胡锦涛满心期望，却一直谴责温家宝是“赵紫阳余孽”，也从反面反映出胡、温根本不是一路人。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;然而，由于受幼稚团体和共特的误导，许多异议人士至今仍以为“胡温”是共同进退的同盟体，进而总以“胡温”一词来称谓分歧重大的胡锦涛和温家宝两股政治 势力，这种看法实在是糊涂。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;共特之所以一再放出胡温一体的“内幕消息”，目的无非是为胡锦涛极权倒退打掩护、并借以捆绑温家宝。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;也有看穿胡锦涛的人，因为胡锦涛的倒行逆施否定温家宝，认为温家宝对自由民主、普世价值的肯定纯属作秀，是“事先安排好的分工”，胡温两人，“一个唱红脸，一 个唱白脸”，他们并以毛泽东与周恩来的配合为例。这种看法似是而非，因为一个急于立威的左棍总书记，不可能去树一个唱反调的总理。当年周恩来的作风看似 “人性化”，实则把毛泽东的毒药裹上蜜糖推销，中共建国后周恩来从未有过公开唱毛泽东反调的纪录。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;事实恰恰相反：温家宝不仅不是什么胡锦涛的同盟者，相反，现在还是胡锦涛势力全力打压、并且首先要排挤掉的对手。这，可以从温家宝被封杀、被丑化、如今势单力孤的声音可以看出来。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;中共国的政府总理，一无枪杆子二无笔杆子，本来就没有什么实权。当年江泽民为了排挤朱F基，削夺了总理的政 法权，把朱总理搞成“经济内阁”，胡锦涛上台以后竭力加强党权，政法委权力全面扩张，总理权力进一步萎缩；从十七大开始，胡锦涛已经派他的铁杆亲信李克强 占据了温家宝接班人（常务副总理）的位置，强势“辅佐”国防、能源、经济结构调整等重大领域、又让与己臭味相投左棍“张北韩”（张德江）主管“经济改 革”、让亲信刘延东主管教改、分管港澳事务...胡锦涛对温家宝的“掺沙子”动作，终于对内扭转了市场化改革方向，把中国经济拖回了“国进民退”的倒退路 上，对外高举意识形态对抗破旗，逐渐重归与美国敌对老路。权力不断萎缩的温家宝，对此有心无力；可以说，如今的温家宝，连“经济内阁”都算不上，最多算个 半吊子的“经济内阁”――现在的温家宝只在金融上有行事权。胡锦涛让温家宝在金融上有行事权，无非是想让他届时为金融崩溃负责而已。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;胡锦涛一边夺温家宝的权，一边却于今年二月二十七日签署命令，主使全国人大通过的《国防动员法》，似乎赋予国务院总理部分调动指挥军队的权力。这难道要 授予温家宝兵权？当然不是，这是在为他自己的接班人李克强打造强势总理的权力基础，为今后反制江泽民的接班人习近平做准备。加强李克强的总理权，也就是加 强胡锦涛自己的权力，狡猾的胡锦涛，已经在为自己十八大后“垂帘听政”铺路。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;胡锦涛为什么要把温家宝当作全力打压、并且首先要排挤掉的对手？<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;这首先是因为温家宝力主政治民主化改革，温家宝的主张为中国转型所急需，容易引发社会共鸣，深具启蒙的巨大潜力，此种主张，赵紫阳余晖冉冉，当然被一心学 朝鲜、古巴，谋建新极权、打造新铁幕的胡锦涛视为头号心腹大患。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;其次因为，温家宝在统治集团上层力量弱小，容易排挤：温家宝的政治改革主张，威胁到以江泽民为总代表的官僚资本既得利益集团利益，江泽民等人尤其害怕一 旦政治民主化，其本人和家族会被法轮功清算；因此，江系势力必然支持胡锦涛排挤温家宝。温家宝成了中共高层中最软的柿子，“柿子挑软的捏”，向来“防微杜 渐”、惯捏软柿子的胡锦涛，当然要先吃温家宝。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;如果这种情形不变，一旦中共国经济在“十八”大前出大问题，温家宝一定会被推出来作替罪羊，成 为继赵紫阳之后，“改革开放”三十年来第二位“犯下严重错误”而被撤销职务的高层领导人。当然，温家宝也有反击胡锦涛的机会，因为胡锦涛主导的极权倒退， 在整肃刘晓波们的同时，也一定会损害以江泽民为总代表的官僚资本既得利益集团的利益，胡、江在“反自由化”上的结盟不可能巩固。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;在邓小平等强势元老死后，由于权力交接班游戏规则迟迟不能产生，中共政权，无可避免地在愈演愈烈的内斗中走向混乱和解体。<br />
