													<h2>李泽厚 易中天：警惕民族主义与民粹主义合流</h2>
						
						
																											<div id="postmessage_835849" class="t_msgfont">著名文化学者李泽厚、易中天对话录<br />
作者：李泽厚,易中天<br />
来源：新京报评论周刊<br />
来源日期：2010-9-18<br />
<br />
　　李泽厚.著名哲学家，上世纪八十年代最知名学者，曾任中国社会科学院研究员，德国图宾根大学、美国威斯康星大学、密歇根大学、科罗拉多学院、斯瓦斯摩学院客席教授、客席讲座教授，台北中央研究院客席讲座研究等职。1988年当选巴黎国际哲学院院士，1998年获美国科罗拉多学院荣誉人文学博士学位。著作《批判哲学的批判―――康德述评》、《中国（古代、近代、现代）思想史论》、《美的历程》等影响巨大。<br />
<br />
　　易中天,著名文化学者，厦门大学人文学院教授、博士生导师，著有《艺术人类学》、《帝国的终结》、《先秦诸子百家争鸣》、《费城风云》、《我山之石》、《中国智慧》等。<br />
<br />
　　民族主义加民粹主义，是当前中国往何处去的最危险的一个方向，大讲“中国模式”就有这个危险<br />
<br />
　　今天，恐怕没人不知易中天，但李泽厚呢？曾有文记载，李先生南下做客某书店，青年学子奔走相告：“太好了，李泽楷要来了！”<br />
<br />
　　人们真是善于健忘。李泽厚先生《美的历程》一书在上世纪80年代是何等受追捧，这位当选巴黎国际哲学院院士的中国人，曾是当年知名度最高的学者。<br />
<br />
　　我们真的已走过李泽厚这座桥？<br />
<br />
　　9月12日，在李泽厚先生家中，他与易中天进行了一场对话，谈古论今，纵横捭阖。<br />
<br />
　　■ 背景<br />
<br />
　　易中天:三见李泽厚<br />
<br />
　　李泽厚先生说，我们是第三次见面吧？<br />
<br />
　　这是2010年9月12日，我到北京去见先生。“80后”的先生身着燕居之服，神清气朗，腰板笔挺，站在家门口，一边握手，一边问我。<br />
<br />
　　第三次？我一下子算不过账来。<br />
<br />
　　先生说，是。上次是90年代，在厦门。再上次，是80年代，在武汉，你送我去火车站。当时我感觉你很压抑。<br />
<br />
　　李泽厚先生的记忆力惊人。80年代，我确实见过他。还跟刘纲纪先生一起，把他从武汉大学送到东湖宾馆。那时没有车，连出租车都没有，是走去的，权当散步。但我是“小字辈”，没有插嘴的份。因此，大抵是泽厚先生和纲纪先生，肩并肩在前面边走边谈。我跟随在后，算是“执弟子礼”。后来，90年代在厦门，有过一次座谈，我也只是旁听而已。因为提问的是学生，我不便问；答疑的是先生，我也不便答。<br />
<br />
　　所以，这回见面，我就问得比较多，也发表意见（这些对话，《新京报》的赵继成已经整理出来发表，不再重复）。整个过程，仍然是我问得多，说得少；先生则谈兴甚浓，有问必答。论古今，谈天下，指点江山，臧否人物，直言不讳，口无遮拦。说到精彩处，妙语连珠，神采飞扬，旁若无人，让在座者无不暗自惊叹。<br />
<br />
　　后来去吃饭。在电梯口，我说：送给先生的书中，有一篇《盘点李泽厚》。<br />
<br />
　　先生哈哈一笑，说我早就看过了。<br />
<br />
　　我也笑。其实，我是“明人不做暗事”。《盘点李泽厚》收入云南人民出版社2001年版《书生意气》时，我就曾托人带去一本，但杳无音信，不知先生是什么时候看到的。<br />
<br />
　　先生说，2002年就看到了。<br />
<br />
　　啊，记得如此准确！<br />
<br />
　　我说，如有不恭之处，请先生海涵！<br />
<br />
　　先生说，你可以质疑，你可以批评，没有任何关系！然后，先生看定我，说：不过，你的观点，我不同意！<br />
<br />
　　这倒是意料之中的。只是不知道先生的“不同意”，是不同意我说他“老了”，还是不同意我质疑他的“西体中用”。前一条，我可能是错了，因为他的心灵确实还很年轻。至于“西体中用”，我则保留看法。没准，将来还会撰文与先生商榷。<br />
<br />
　　先生还对我说：你想批评我，就批评；我该支持你，还支持。不过，我对你有两个建议。说完这两个建议，先生忽然停下脚步，又看定我，说：你看，我很真实吧！<br />
<br />
　　哈，李泽厚到底是李泽厚，湖南人毕竟是湖南人。于是想起他客厅里冯友兰先生所赠的那副对联：西学为体，中学为用；刚日读史，柔日读经。指着这对联，李泽厚先生不无得意地对我说：哈哈，都是反过来的。<br />
<br />
　　确实都是反过来的。前人的说法，是“中学为体，西学为用”，和“柔日读史，刚日读经”。但让我景仰的，还不是那“反过来”，而是先生说这话时的神态。<br />
<br />
　　于是得出“李泽厚印象”云──<br />
<br />
　　居然宝刀不老，<br />
<br />
　　依旧性情中人。<br />
<br />
　　谈孤独<br />
<br />
　　不喜欢扎堆、抱团<br />
<br />
　　易中天：先生有孤独感吗？<br />
<br />
　　李泽厚：当然有，而且比较强烈。人本是社交动物，有社交的本能和欲望，但我的个性是不太喜欢与人交往。我有三个先天性毛病，与此个性恶性循环：一是记不住面孔，这已有专文讲过；二是记不住声音，别人打电话我总要问“哪位”？有次包括我儿子，所以他现在总是先报上名来；三是记不住路。在五六十年代则被扣过三个帽子，当时是很严重的，一是不接近群众，二是不靠拢组织，三是不暴露思想。一个是个性问题，一个是不符合革命时代要求的问题，两者性质不同，但也有联系。<br />
<br />
　　我曾在香港呆过一年，离开后没主动和任何人联系过，我在台湾呆了大半年，离开后没有任何联系，在国内在美国也都大体如此，这是个性，从小见不得生人，总往后躲，大人骂我“出不得世”。几十年散步都是一个人，连太太也不让陪。<br />
<br />
　　易中天：我能体会。我为什么问这个问题，因为我自己也有这个问题。我其实不喜欢扎堆、抱团，参加什么组织和团体，反倒喜欢一个人呆着。至少，得有独处的时候。带个助理，走哪儿跟哪儿，我受不了；前呼后拥，人来人往，更受不了。<br />
<br />
　　李泽厚：你现在是没有办法了，你走到哪里别人都能认出你来。我若这样会浑身不自在。几年来，我有了个“三可三不可”原则：可以吃饭不可以开会，可以座谈不可以讲演，可以采访、照相，不可以上电视。因为后者太正式，前者都属聊天，愿意聊什么就聊什么，随意得很。至于上电视，我想是“语言无味、面目可憎”。不像你，易中天，风趣幽默，还一表人才，哈哈！<br />
<br />
　　谈哲学<br />
<br />
　　书生要有责任感<br />
<br />
　　易中天：先生曾谈到对国内可能产生纳粹思潮的担忧，能否具体讲讲？<br />
<br />
　　李泽厚：我今年七月份去欧洲走了一趟，专门到波兰看了奥斯威辛集中营，这个地方我一直想去看，以前特地去华盛顿看过奥斯威辛集中营的一些实物资料，这一次想到实地去看看，这毕竟是20世纪最大事件之一。<br />
<br />
　　看到的毒气室、焚尸炉比我想象的要小，在现代科技运用下却可以短时间内消灭几百万人，太可怕了。不仅是犹太人，还有苏联的战俘、共产党人、吉卜赛人。纳粹鉴别“纯雅利安人”和“非雅利安人”，还有一套“科学”的方法，如测量面骨等数据。<br />
<br />
　　易中天：这是“人种学”的所谓“科研成果”。<br />
<br />
　　李泽厚：当时我就想，一个邪恶的理论，而且是非常肤浅的理论，一旦忽悠了群众，和权力结合――希特勒可是通过选票上台的――可以造成多么巨大的灾难，由此可以推论出理论工作的意义，即反对邪恶的理论、思潮、思想非常重要。<br />
<br />
　　我以前常说书生百无一用，现在倒认为书生还真要有点历史感和责任感，要对自己所写所说负责，不要把人们引入错误方向。现在有此危险。<br />
<br />
　　易中天：以前有句话，叫“真理为广大人民所掌握，就会变成无穷无尽的力量，变成威力无比的精神原子弹”。<br />
<br />
　　李泽厚：列宁也讲，“没有革命的理论，就没有革命的行动”。<br />
<br />
　　海德格尔的哲学思想是在希特勒之前，我曾称之为士兵的哲学，是向前冲锋、向前行动的哲学，海德格尔哲学在“二战”时被纳粹以物质力量填补他那个面向死亡前行巨大的深渊，个体生命的意义成了罔顾一切只奉命前冲的士兵的牺牲激情和动力。我觉得他在政治上与纳粹的关系还是次要的，他的思想在深层次上很危险更为重要。<br />
<br />
　　易中天：那有一个问题，为什么那么多人喜欢海德格尔？为什么那么多人喜欢“现代、后现代”？为什么如今的学术界如此抛弃西方古典哲学，不讲康德，不讲黑格尔？<br />
<br />
　　李泽厚：中国学术界有一个大问题，就是赶国际时髦太厉害。<br />
<br />
　　海德格尔在哲学上确实有成就，他把死亡问题提了出来，只有死亡对你是不可替代的，是独一无二的，从而这个时代最鲜明的特征即个体的重要性被分外突了出来，所以有吸引力。同时，启蒙在西方也确实遇到了困境，理性主义在西方产生了很多问题。我在美国生活，美国不是十全十美的，问题很多很大。于是好些人喜爱种种反理性的哲学以追求生存的意义。<br />
<br />
　　谈思想<br />
<br />
　　警惕民族与民粹主义<br />
<br />
　　易中天：先生怎么看自由主义和新左派？<br />
<br />
　　李泽厚：《己卯五说》“说历史悲剧”文中我说过与这两派的异同。十年来，我的基本看法没变，但也有一些变化，那就是跟新左派的距离越来越远。<br />
<br />
　　我和自由派有理论上的不同，比如我不同意“天赋人权”、“原子个人”等等，我认为这是非历史性的假定，我是历史主义者，不同意。也有实践上的不同，比如我从上世纪80年代起就一直不赞成目前在中国搞一人一票的总统普选、多党、议会制，这样会天下大乱。<br />
<br />
　　易中天：为什么不同意“天赋人权”？<br />
<br />
　　李泽厚：“天赋人权”是近代才提出来的，古希腊没有，原始社会更没有，封建社会也没有，因此我还是同意马克思的说法，工业社会经济的变化使得雇工、农民开始出卖劳动力进入市场，这才是前提，由身份制到契约制等等，由此衍生出包括人权在内的各种思想，所以，根本不是什么“天赋人权”，自由主义的这个假定如同专制君主的“王权神授”一样，在理论上不正确。<br />
<br />
　　但某些不正确的理论在特定条件下可以有好作用，如“天赋人权”在启蒙、在开民智上就如此。所以我非常赞同自由主义强调的世界共同追求的价值必须坚持。对社会的批判目前重点也仍在反封建反专制，这非常需要，因为今天在经济迅猛发展成绩斐然之下掩盖了很多问题和某些领域内的停滞和倒退。<br />
<br />
　　我不同意一些自由派认为的现代化就是美国化，我认为中国要走一条自己的路，这条路如果走好就是对人类最大的贡献。因为中国有13亿人口，如果完全美国化，对人类是个巨大的灾难。因此，当年新左派提出中国走自己的路，我非常赞成，但十来年他们要走的自己的路是照搬西方的“后现代、后殖民主义、文化相对主义”，后来又和新儒家、新国学结盟，高唱民族主义等，我就非常不赞成了。<br />
<br />
　　易中天：有人还把我划进新国学，我到处声明我可不是，我是被强行划进去的。可是他们根本不听，还以为说你国学是抬举你。<br />
<br />
　　李泽厚：有人还把我划进去呢。什么是国学？我的著作中从来不用这个词，因为这个概念本身不清楚。有些搞国学的人大讲“三纲”，公开主张专制等等，如果再和“中国可以说不”“中国不高兴”等相结合，最容易煽起群众性的民族情绪，这很不好。<br />
<br />
　　易中天：现在要是有人提和日本、美国干一仗，他们都群情激昂，都愿意。<br />
<br />
　　李泽厚：均贫富、倡平等、一人一票直选总统的民粹主义和儒学最优、传统万岁、“中国龙主宰世界”的民族主义一相结合，其中包括新老左派、后现代与前现代的合流，假如变成主导的意识形态，便非常危险，它将对外发动战争，对内厉行专制。<br />
<br />
　　民族主义加民粹主义，正好是“国家社会主义”，即纳粹，这是当前中国往何处去的最危险的一个方向，大讲“中国模式”就有这个危险。<br />
<br />
　　谈法治<br />
<br />
　　天赋人权理论上不对<br />
<br />
　　易中天：先生反对民族主义加民粹主义的“纳粹倾向”，我完全同意，举双手赞成。我也反对讲“国学”，反对所谓“儒家社会主义”，更反对定儒学为“国教”。中国道路，不是这么走的。但我感觉先生好像有点冤枉了自由主义，自由派也不都认为现代化之路就是美国之路。<br />
<br />
　　李泽厚：同一个派别，人与人也不一样。必须具体人物具体对待，对自由派、新左派、国学派都应该这样，这里篇幅不够只好笼统而言之。<br />
<br />
　　易中天：先生说根本就没有什么“天赋人权”，那么请问先生主张“什么人权”？<br />
<br />
　　李泽厚：“天赋人权”理论上是错误的，但现实中是有用的，作为策略可以讲，对人权有好处。<br />
<br />
　　易中天：但如果我们想彻底一点，策略上可以讲，理论上也可以用，有没有替换的提法？<br />
<br />
　　李泽厚：当然有，我一直讲我们每个人都有人权，这个人权不是天赋的，而是人类历史发展到一定阶段必须要有的。<br />
<br />
　　易中天：可不可以说“人赋人权”？<br />
<br />
　　李泽厚：当然可以。<br />
<br />
　　易中天：那到底是“人赋人权”，还是“法定人权”？<br />
<br />
　　李泽厚：这只是词的问题。<br />
<br />
　　易中天：不，还是要分清楚。<br />
<br />
　　李泽厚：我主张法治，所以上世纪八十年代晚期我不同意王元化他们提出“新启蒙”，因为现在不是要再煽起来一个启蒙性的群众运动，而是要把人权切实落实到制度上，也就是法治。从八十年代后期起，我一直强调法治，例如强调程序法，强调形式正义的重要，强调落实法治的具体措施，等等。<br />
<br />
　　我看到一个材料，一个管政法的书记，最近竟然说，“那些法律条条不要管它”。这种思想是有来历的，1954年第一部宪法通过，到1958年，就有高层领导直接讲“不要什么刑法、民法，我们开会就是法律”，“法律束缚了我们的手脚”。<br />
<br />
　　所以，现在讲人权、讲民主，主要是要通过法律确定各种具体的规范条例，并且坚决执行。我讲有三点必须做，第一要党内民主，第二要鼓励舆论监督，第三要独立审判、检察也要独立。中国太大，情况复杂，这些可以慢慢做，渐进，累积。现在的大学教授很多都在搞假大空的项目课题，以获取经费，这不能责怪教授，是体制造成的。反倒是好些媒体每天都在做，每天都在推动，所以我看重媒体。<br />
<br />
　　谈大学<br />
<br />
　　应“重建象牙之塔”<br />
<br />
　　易中天：这也是我十几年来想的一个问题。从大学学术量化管理那一天开始，我就决定走一条自己的路。我不要填那些表格，我不想说自己不想说的话，我不想写自己不想写的字，也不想申请什么别人规定的“课题”。<br />
<br />
　　李泽厚：但没有多少人能像你这样冲出来，不容易。<br />
<br />
　　易中天：不管是谁，也都要养家糊口过日子啊！所以，我还是坚持十年前的那句话：没有经济独立，就没有人格独立；没有人格独立，就没有思想独立。<br />
<br />
　　李泽厚：也有人辛辛苦苦地在做学问，坐冷板凳，不管东南西北风，却非常清贫。<br />
<br />
　　易中天：这样的人我很敬佩，但不是所有的人都能做到。正所谓“墨子独能任，奈天下何”？何况清贫也不等于一文不名，基本的生活总要有保障。陶渊明“不为五斗米折腰”，是因为还可以“种豆南山下”。一旦上无片瓦，下无立锥之地，餐餐饭都要靠别人施舍，想到庙里挂单都不行，有几个人能不被收买？人，总有扛不住的时候，除非下定决心饿死在首阳山。可是，就算你自己扛得住，老婆孩子呢？要不要管？<br />
<br />
　　李泽厚：所以，现在应提出“重建象牙之塔”，这也要有巨大资金保证才行。<br />
<br />
　　易中天：重建象牙之塔，十分必要。巨大资金保证，也很重要。但关键是“象牙塔”里的人，不能有“后顾之忧”，更不能“卖论求资”，靠出卖观点去获得“资金保证”。所以我强调“经济独立”。经济独立，不等于“富可敌国”，只不过是“不必看人脸色”。这跟“安贫乐道”不矛盾。一个人再清贫，只要那为数不多的钱是自己的，照样可以保持“人格独立”。<br />
<br />
　　谈改革<br />
<br />
　　始终是“审慎的乐观”<br />
<br />
　　易中天：现在有一个非常严重的问题，大量民间资金外流，移民成为一个新潮流，其中大量是精英。有人写文章，说现在是资本和思想在移民，我不知道剩下什么了，请问先生，希望在哪儿？<br />
<br />
　　李泽厚：我的观点仍然是“审慎的乐观”，中国那么多人，不是每个人都可以跑到外面去的，哪怕是既得利益者，他们也不是一个人两个人，牵涉到的人那么多，不可能都移民，所以他们也并不希望社会越来越坏，因为也对他们不利。<br />
<br />
　　应该想的是，即便在这样的情况下，我们还可以做些什么？这就叫“知其不可而为之”。<br />
<br />
　　易中天：我再问先生一个问题，先生既不赞成自由主义的美国化，也不赞成新左派的纳粹化，那么请问，中国道路应该怎么走？<br />
<br />
　　李泽厚：中国道路怎么走，我仍然赞成邓小平说的摸着石头过河，我认为目前应该提出“中国不应该往哪个方向走”。<br />
<br />
　　易中天：太好了！先生的意思是说，重要的不是“中国往哪走”，而是“不往哪走”。我也赞成先生的小步前进，但问题是，你小步前进，如果触动到一些东西前进不了了，怎么办？<br />
<br />
　　李泽厚：十年前，我就和新左派口头激烈争论过，他们认为中国会崩溃，不能加入WTO，等等，我认为不会，一定要加入WTO，现在他们已经完全改变过去的说法了。<br />
<br />
　　美国也有人说“中国崩溃论”，出过著名的书，但现在他们也无话可讲了。<br />
<br />
　　易中天：我也认为不会崩溃，但就怕遇到一个硬要焚烧古兰经的牧师，咋办呢？<br />
<br />
　　李泽厚：好像不烧了。因为对方提出不在9・11遗址附近建清真寺，他也不烧了。你看，总会有妥协的办法。但我不知道这个讯息是否准确，没去核对。<br />
<br />
　　所以，我始终是“审慎的乐观”，以前是，现在还是，只是有时“乐观”多一点，有时“审慎”多一点。现在经济大发展了，某些领域却停滞或倒退，经济领域中也有严重问题，所以要审慎。<br />
<br />
　　谈国学<br />
<br />
　　“文化相对主义”错误<br />
<br />
　　易中天：还有个问题，为什么思想界那么多人往左转？<br />
<br />
　　李泽厚：贫富不公、社会矛盾凸显是很大一个原因，官商一体对社会伤害非常大，贫富分化过速过大确实要好好解决。<br />
<br />
　　易中天：先生注意韩寒吗？<br />
<br />
　　李泽厚：我知道这个人，我最欣赏的是他赛车，赛车是会丢掉性命的，但他成绩非常好，一定不容易，对这一点我愿表示敬意，其他不说，因为完全不清楚。<br />
<br />
　　易中天：陈文茜骂韩寒知道吗？<br />
<br />
　　李泽厚：不知道。<br />
<br />
　　易中天：为什么一些人在左转？我认识的一个人，以前是彻底反传统的，他曾跟我说中国的传统包括唐诗宋词都要彻底否定，结果现在都转向了。<br />
<br />
　　李泽厚：因为国学热嘛，经济发展了就以为什么都了不起，自卑又自大的民族情感嘛，鲁迅早说过。<br />
<br />
　　以前反传统的时候，他们骂中国文化骂得一塌糊涂，对传统一概否定，那时候我写《中国古代思想史论》，多少人骂！现在国学热，我又挨骂，因为我不赞成搞这些东西。我把这叫做“蒙启”，把启蒙再蒙起来。<br />
<br />
　　“文化相对主义”是错误的。文化首先是衣食住行的物质生活，梁漱溟说坐马车比火车舒服，可是你从北京到广州坐马车试试？80年代有外国人对我说，你们中国不要发展汽车，骑自行车挺好，你们知识分子上山下乡也挺好，我说你愿意骑自行车从纽约到华盛顿吗？你现在还愿意夏天没有空调、冬天没有暖气吗？恐怕大多数人不愿意，这里没有什么相对。<br />
<br />
　　我的“吃饭哲学”是批评文化相对主义的重要武器。15年前我提出来现代化的四个顺序：经济发展、个人自由、社会正义、政治民主，首要的是发展经济，当时多少人骂我啊。<br />
<br />
　　本版记录整理/本报记者 赵继成</div>

							
							
							
							
