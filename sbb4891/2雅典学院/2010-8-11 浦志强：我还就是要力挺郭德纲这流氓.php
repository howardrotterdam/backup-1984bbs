													<h2>浦志强：我还就是要力挺郭德纲这流氓</h2>
						
						
																											<div id="postmessage_778908" class="t_msgfont">看不惯对郭德纲饱以老拳，说了几句反动话。网友为力挺这流氓对我失望，我也就有了澄清的必要。<br />
&nbsp; &nbsp; 小题大做大事化小再把小事化了，堪比报菜名儿那类老段子，是个人张开嘴就能说几句，但要说好得要有真功夫。眼下对郭德纲的围剿，在我看就是一段报菜名儿，只是不光厨子的火候儿差，把色香味烩得没品，跑堂儿的也不着调，最终是客人不买单，餐馆儿老板得自掏腰包蚀这把米。<br />
&nbsp; &nbsp; 郭德纲是不是流氓，见仁见智，杜月笙还流氓大亨呢，但离开了梅兰芳的孟小冬，偏偏就嫁了他。社会是多元的人是多面的，庙堂外得有江湖，有江湖才有社会，才谈得上和谐，因为江湖就是社会。否则就是病态，纳粹德国、文革中国和金氏北韩，全这副德行。<br />
&nbsp; &nbsp; 郭德纲就一艺人，靠的是功夫，他没攒过《生死抉择》，顶多给《建国大业》串了个龙套，雅俗共赏主要是“三俗”，德云社跟主旋律不搭界。艺人吃的是开口饭，上千块一张票没人逼着你买，都是衣食父母掏钱捧他的场，官府可没给他出盘缠――官人能让党买单，他不该背黑锅。姜昆要是有这能耐，你标一万啊？尝尝你们自己吣的那滩东西，甜的发腻没辣子一点儿不开胃，卖十块钱给茶水贴路费，外带抽奖看《孔繁森》和《任长霞》，照样门可罗雀没人搭理你，但你怪得着郭德纲这流氓吗？<br />
&nbsp; &nbsp; 师徒反目同行踩乎，哪个行业都这样概莫能外，但凡有人跟着也就有了是非。徐德亮李菁何云伟退社，是人家自己选择，好合好散散不好有衙门伺候着，经官断个明白谁都别撒钱买，也就是了。听相声大家解解闷儿说别的又不合适，想高雅你去维也纳听新年音乐会呀？五十六个民族五十六句话，那儿天天都是好日子，想爱我中华，人家收了钱就肯管你够。<br />
&nbsp; &nbsp; 德云社不是精神文明建设基地，郭德纲那帮徒子徒孙，估计没时间学习三个代表，姜昆能立竿见影指定学了，但八成儿急用先学浅尝辄止没法儿联系实际带着问题学，所以无论雅俗，这相声界未必有人真懂啥叫钦定的八荣八耻。但是江湖事江湖了，这是规矩是明晃晃的规则，动不动就引回纥兵解长安之围，姜昆这样学郭子仪，他不厚道。<br />
&nbsp; &nbsp; 咱这儿英明领袖辈出，比英雄出得还稀烂。人不管啥样，戴顶绿帽就成了少将，上灶台就敢英明的埋锅造饭，把行头刷成绿色儿，就敢号称三军统帅，恶心人的事儿多了。局子里要是没得矛盾，那高岗彭德怀二月逆流会打倒吗？刘少奇和赵大爷会出局吗？那秀才文胆田家英会自杀吗？那四人帮再祸国殃民用得着粉碎吗？而陈良宇也早该英明地升了官了，陈希同又何苦蹲十好几年？<br />
&nbsp; &nbsp; 学界要是没龌龊，赵秉志就不会跟人民大学失和，跑北师大另立山头。郭德纲再操蛋，也就嘴上功夫。但他那是真功夫，那是他真有功夫。他讨好衣食父母，让别人浑身刺挠，是招人不待见，但他把谁家孩子扔井里啦？贪了赃还是卖了法？走私了军火还是拉动了美国内需？抄了王天成段子进政治局出堂会，还是跑西太平洋大学拿仨博士学位回来，再把自己当表演艺术家供着啦？让十几年前的学界名流何新，拿教授德行跟郭德纲比比，一目就能了然。<br />
&nbsp; &nbsp; 要认清官府对一个艺人围剿，实质究竟是什么。一个庸俗无比的世道，最俗的人才好专整这套反三俗的烂景儿。流氓当道街上像流氓的人多了，但谁是真流氓谁他妈真流氓，还得拉出来遛遛得骑驴看唱本儿。<br />
&nbsp; &nbsp; 我就喜欢郭德纲这股江湖气，我就是要力挺这流氓，哪怕有一天他扛不住，低头认怂了。<br />
&nbsp; &nbsp; 浦志强 2010年8月11日 沈阳<br />
