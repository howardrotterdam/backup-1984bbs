<br />
乱云飞渡仍从容――一阵激烈的云雨之后，江青仍然气定神闲、从容不迫。（或指毛的那活儿仍然坚挺）。<br />
<br />
天生一个仙人洞――一个多么美好的、令人陶醉的“仙人洞”啊！（或者可以理解为江青的床上功夫十分高超，令毛泽东欲仙欲死，忍不住赞叹江青的那个仙人洞）<br />
<br />
无限风光在险峰――还有更美妙的地方，是江青胸脯上的那对乳峰！<br />
<br />
更为奇特的是，据李志绥的《毛泽东私人医生回忆录》披露，毛泽东看到中宣部那班无耻犬儒将他这首淫诗拔高为“革命乐观主义精神”和“昂扬的革命斗志”之后，只是大笑不已，而不许人说破。<br />
