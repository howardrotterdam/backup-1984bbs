松树在暮色苍茫中傲然挺立在山崖上，一阵阵乱云从容地飞过。天设地造好一个仙人洞，正是在这险峻的山峰上才能领略到无限美好的风光。 　　??此诗为毛泽东于一九六一年所作，虽然所描写的场面较小，但却体现出了伟人从容不迫的心态。六十年代我国面临着极为严峻的国内外形势，错综复杂的内外矛盾交迫，使国内局势出现恶化，国家困难重重，这是考验国人的关键时期，而作为国家领袖的毛泽东依旧表现出在大革命最艰难时的宏伟气魄，泰然处之，以积极主动的心态迎挑战而上。伟人以高超的写作技巧将风云变幻的国际形势浓缩于此七绝之中，从中可以领略到作者“任凭风浪起，稳坐钓鱼台”的悠闲自得的超然风度。 　　??松树一向就是被国人视作坚强不屈、不怕压迫、敢于反抗、敢于斗争的精神象征，在暮色余辉中，更能激起人们对它的敬佩。即使是乱云飞渡而来，它仍然从从容容，若无其事，显示了“劲松”不怕被包围的坚强风格。作者将当时的形势比作“暮色”，“劲松”比作自己以及敢于坚持真理、不惧外来压力的中国共产党人和伟大的中国，涵盖深远，蕴魅无穷，给人以无限的想像空间，甚至于激起读者汹涌澎湃之激情、临危不惧之遐想。 　　??“苍茫”为“无边无际、模糊不清”之意，“暮色苍茫”表当时的国际形势变幻莫测，但“劲松”依然刚劲挺拔，从容不迫。当时毛泽东面临具大压力，国内由于天灾人祸带来的空前经济困难，使他受到来自各方面的指责。在国际环境中，美国第七舰队不断在台湾海峡游弋，核潜艇经常出没于日本冲绳海域，严重威胁着我国安全，而苏联方面又因意识形态领域的分歧而背信弃义地扩大对我的经济制裁，使得我国的发展难上加难，雪上加霜。如何这对待一复杂局势？这的确是个极大的考验，决不亚于情势严峻的大革命时期。 　　??一个人精神上究竟能承载多大的极限负荷呢？又该怎么承受来自各方压力呢？而毛泽东仍然镇定“从容”，似乎是渴望地等待那激烈场面的到来，甚至很感兴趣地主动迎击而上，好“其乐无穷”地与之奋斗，一如既往地发挥他的斗争哲学，“梅花喜欢漫天雪”正合此意。 　　??“天生一个仙人洞”亦实亦虚，虚实结合，从写意中取寓。“无限风光在险峰”可以说是对诗人一生的人生哲学的概写，在“暮色苍茫”的环境中，他独能登临“险峰”，看“劲松”，观“乱云”，寄托着深遂而丰富的哲理的思考。将所描写之实物注入自己的意志，使人领会奇异于平淡之中。正如马克思所说：“在科学上面是没有平坦的大路可走的，只有在那崎岖小路的攀登上不畏劳苦的人，有希望达到光辉的顶点。”（《“资本论”法文本的序和跋》）使我们不难联想到在帝国主义反华的重压下，中国共产党人纵观五洲风云的“从容”人英姿和伟大气魄。 　　??诗人能将复杂的社会局面同自己的意志寄寓于短短的28个字，足见其卓越的文学造诣。“劲松”、“乱云”、“从容”都具有特殊的象征意义，“神用相通，情变所孕；物以貌求，心以理应”（刘勰语），此诗典型的艺术风格构造出不凡的意境，可以让人思维在此意境中任意驰骋，给人以刚键奇特之美，“吟咏之间，吐纳珠玉之声；眉睫之前，卷舒风云之色。”（《文心雕龙・神思》） 　　??另，至于文学界对是“劲松”“从容”还是“乱云”“从容”之争，其实大无必要，诗人既以“劲松”自比，“乱云”意为国内外形势，很明显“从容”固理当为“劲松”之“从容”，就是显而易见的，有人说是“乱云”“从容”于情于景皆不妥，胡乔木先生之观点仅为“个人的一点看法”，可作参考。但对于这样一首有严谨组合的诗来说，应当谨慎对之，深刻理会诗人之意，当为“劲松”于“乱云”之中“从容”。 　　??就是诗人有“从容”之心态，才有“无限风光在险峰”，也不难想像，当时全国人民在黑暗中摸索，在崎岖小路上攀登，在艰难险阻中寻求治国自强的真理，并表现出浩浩傲然之骨气，正是有这样的领导集团，才得以顶住了强大的外来压力，战胜了来自国外的挑战，使国内经济建设事业得以在困难中继续向前推进，“不畏劳苦的人”正在“劳苦”中苦苦寻觅。 　　??毛泽东正是有着这超然的气魄和智慧，才能将前苏联与美国两个超级大国玩弄于手掌之间，巧妙利用通过博弈所赢得的空间化解了许多危机和国内纷争，为新生的共和国赢得了自信。 　　??（这首诗最早发表在人民文学出版社一九六三年十二月版《毛主席诗词》。 　　?? 仙人洞，在庐山佛手岩?a target=_blank href=/view/4157.htm&gt;岭之约两丈，深广各三四丈，传为唐朝仙人吕洞宾所居，故名。李进即江青。）</div>

							
							
							
							
