<br />
按说，美国对日共有再生之恩，日共应该对美国感激不尽才对。然而信仰共产主义的日共，和美国有根本价值观和意识形态上的冲突，再加上韩战的爆发，使得听命于苏联，支那的日共和美国迅速对立，成为日本国内最反美的左翼政党之一。日共虽和支共一样都是共产党，但毕竟不同于靠打家劫舍，杀人放火起家的支共流氓匪类，对于赤裸裸的暴力革命仍然有许多的顾忌。60年代，毛泽东多次指示日共采用暴力夺权的方式在日本开展武装斗争，推翻日本政府，粉碎天皇制度。不料却遭到日共总书记宫本显志等人的反对。宫本坚持走民主议会路线，用选举的方式获取政权，被毛泽东骂为“反动修正主义”。 <br />
<br />
1966年文革开始，毛不仅号召支那国内的红卫兵痛批宫本修正主义，还发动日共党内的毛派分子“造反”，向宫本夺权。同年8月日共驻北京的最后两名代表离开北京，走时还被红卫兵痛殴了一顿。日共和支共就此正式决裂。然而毛并没有放弃他的日本“解放计划”，转向另一个日本左翼政党社会党物色代理人，黑田寿男就是其中之一，黑田也就是小资们经常用来批判毛的经典文章《毛泽东感谢日本侵略》一文中的黑田，黑田是搞农民运动起家的，和毛有共同语言，很快就成为坚定的毛派。在党内为毛大肆鼓噪，使得日本社会党中的毛派也逐渐增多。然而好景不长，70年代随着日本经济快速成长，人们不再轻易被左派所迷惑，日本的学生运动和工人运动也逐渐走下坡路。但是有一些极端顽固的毛共分子仍然不甘心他们的失败，他们成立了赤军，在日本及全世界策划恐怖活动，躲在深山中搞游击战争，劫机绑架人质，甚至企图阴谋暗杀天皇。1972年，日本中央学院大学西内雅教授出访亚洲各国时，偶然得到了一部支共的机密文件即《日本解放第二期工作要V》，在这份机密文件上，支共详细地制定了对日“解放”工作的具体行动方案和实施办法。毛对日本的野心，由此可见一斑。 <br />
