													<h2>我把儿子送给美国佬 竟然这样被教育</h2>
						
						
																											<div id="postmessage_839264" class="t_msgfont">此文为转载，原始出处无法得知。<br />
<br />
当我把九岁的儿子带到美国，送他进那所离公寓不远的美国小学的时候，我就像是把自己最心爱的东西交给了一个我并不信任的人去保管，终日忧心忡忡。这是一种什么样的学校啊！学生可以在课堂上放声大笑，每天至少让学生玩二个小时，下午不到三点就放学回家，最让我大开眼界的是没有教科书。<br />
<a href="http://pic1.kaixin001.com.cn/pic/app/84/69/2_71846959_diary.jpeg" target="_blank">http://pic1.kaixin001.com.cn/pic/app/84/69/2_71846959_diary.jpeg</a><br />
<br />
那个金发碧眼的美国女教师看见了我儿子带去的中国小学四年级课本后，温文尔雅地说：“我可以告诉你，六年级以前，他的数学不用学了！”面对她充满善意的笑脸，我就像挨了一闷棍。一时间，真怀疑把儿子带到美国来是不是干了一生最蠢的一件事。<br />
<br />
日子一天一天过去，看着儿子每天背着空空的书包兴高采烈的去上学，我的心就止不住一片哀伤。在中国，他从小学一年级开始，书包就满满的、沉沉的，从一年级到四年级换了三个书包，一个比一个大，让人感到“知识”的重量在增加。而在美国，他没了负担，这能叫上学吗？一个学期过去了，把儿子叫到面前，问他美国学校给他最深的印象是什么，他笑着给我一句美国英语：“自由！”这两个字像砖头一样拍在我的脑门上。<br />
<br />
此时，真是一片深情怀念中国教育。似乎更加深刻地理解了为什么中国孩子老是能在国际上拿奥林匹克学习竞赛的金牌。不过，事已致此？也只能听天由命。<br />
<br />
不知不觉一年过去了，儿子的英语长进不少，放学后也不直接回家了，而是常去图书馆，不时就背回一大书包的书来。问他一次借这么多书干什么，他一边看着借来的书一边打着计算机，头也不抬地说：“作业。”<br />
<br />
这叫作业吗？一看孩子打在计算机屏幕上的标题，我真有些哭笑不得DD《中国的昨天和今天》，这样大的题目，即使是博士，敢去做吗？<br />
<br />
于是我严声厉色地问是谁的主意，儿子坦然相告：老师说美国是移民国家，让每个同学写一篇介绍自己祖先生活的国度的文章。要求概括这个国家的历史、地理、文化，分析它与美国的不同，说明自己的看法。我听了，连叹息的力气也没有了，我真不知道让一个十岁的孩子去做这样一个连成年人也未必能做的工程，会是一种什么结果？只觉得一个十岁的孩子如果被教育得不知天高地厚，以后恐怕是连吃饭的本事也没有了。<br />
<br />
过了几天，儿子就完成了这篇作业。没想到，打印出来的是一本二十多页的小册子。从九曲黄河到象形文字，从丝路到五星红旗……热热闹闹。我没赞成，也没批评，因为我自己有点发楞，一是因为我看见儿子把这篇文章分出了章与节，二是在文章最后列出了参考书目。我想，这是我读研究生之后才运用的写作方式，那时，我三十岁。<br />
<br />
不久，儿子的另一篇作文又出来了。这次是《我怎么看人类文化》。如果说上次的作业还有范围可循，这次真可谓不着边际了。儿子真诚地问我：“饺子是文化吗？”为了不耽误后代，我只好和儿子一起查阅权威的工具书。费了一番气力，我们完成了从抽象到具体又从具体到抽象的反反复覆的折腾，儿子又是几个晚上坐在计算机前煞有介事地作文章。我看他那专心致志的样子，不禁心中苦笑，一个小学生，怎么去理解“文化”这个内涵无限丰富而外延又无法确定的概念呢？但愿对<br />
<br />
“吃”兴趣无穷的儿子别在饺子、包子上大作文章。在美国教育中已经变得无拘无束的儿子无疑是把文章作出来了，这次打印出来的是十页，又是自己的封面，文章后面又列着一本本的参考书。他洋洋得意地对我说：“你说什么是文化？其实超简单DD就是人创造出来让人享受的一切。”那自信的样子，似乎发现了别人没能发现的真理。后来，孩子把老师看过的作业带回来，上面有老师的批语：“我安排本次作业的初衷是让孩子们开阔眼界，活跃思维，而读他们作业的结果，往往是我进入了我希望孩子们进入的境界。”问儿子这批语是什么意思。<br />
<br />
儿子说，老师没为我们感到骄傲，但是她为我们感到震惊。“是不是？”儿子问我。<br />
<br />
我无言以对，我觉得这孩子怎么一下子懂了这么多事？再一想，也难怪，连文化的题目都敢作的孩子，还有什么不敢断言的事吗？<br />
<br />
儿子六年级快结束时，老师留给他们的作业是一串关于“二次世界大战”的问题。“你认为谁对这场战争负有责任？”“你认为纳粹德国失败的原因是什么？”“如果你是杜鲁门总统的高级顾问，你将对美国投原子弹持什么态度？”“你是否认为当时只有投放原子弹一个办法去结束战争？”“你认为今天避免战争的最好办法是什么？”DD如果是两年前，见到这种问题，我肯定会抱怨：这哪里是作业，分明是竞选参议员的前期训练！而此时，我已经能平心静气地循思其中的道理了。<br />
<br />
学校和老师正是在这一个个设问之中，向孩子们传输一种人道主义的价值观，引导孩子们去关注人类的命运，让孩子们学习思考重大问题的方法。这些问题在课堂上都没有标准答案，它的答案，有些可能需要孩子们用一生去寻索。看着十二岁的儿子为完成这些作业兴致勃勃地看书查数据的样子，我不禁想起当年我学二战史的样子，按照年代事件死记应背，书中的结论明知迂腐也当成《圣经》去记，不然，怎么通过考试去奔光明前程呢？此时我在想，我们在追求知识的过程中，重复前人的结论往往大大多于自己的思考。而没有自己的思考，就难有新的创造。<br />
<br />
儿子小学毕业的时候，已经能够熟练地在图书馆利用计算机和微缩胶片系统查找他所需要的各种文字和图像数据了。有一天，我们俩为狮子和豹的觅食习性争论起来。第二天，他就从图书馆借来了美国国家地理学会拍摄的介绍这种动物的录像带，拉着我一边看，一边讨论。孩子面对他不懂的东西，已经知道到哪里里去寻找答案了。<br />
<br />
儿子的变化促使我重新去看美国的小学教育。我发现，美国的小学虽然没有在课堂上对孩子们进行大量的知识灌输，但是他们想方设法把孩子的目光引向校外那个无边无际的知识海洋，他们要让孩子知道，生活的一切时间和空间都是他们学习的课堂；他们没有让孩子去死记硬背大量的公式和定理，但是，他们煞费苦心地告诉孩子怎样去思考问题，教给孩子们面对陌生领域寻找答案的方法；他们从不用考试把学生分成三六九等，而是竭尽全力去肯定孩子们一切努力，去赞扬孩子们自己思考的一切结论，去保护和激励孩子们所有的创作欲望和尝试。<br />
<br />
有一次，我问儿子的老师：“你们怎么不让孩子背记一些重要的东西呢？”老师笑着说：“对人的创造能力中有两个东西比死记硬背更重要：一个是他要知道到哪里里去寻找所需要的比它能够记忆的多得多的知识；再一个是他综合使用这些知识进行新的创造的能力。死记硬背，就不会让一个人知识丰富，也不会让一个人变得聪明，这就是我的观点。”<br />
<br />
我不禁记起我的一个好朋友和我的一次谈话。他学的是天文学，从走进美国大学研究所的第一天起，到拿下博士学位的整整五年，他一直以优异的成绩享受系里提供的优厚奖学金。他曾对我说：“我觉得很奇怪，要是凭课堂上的学习成绩拿奖学金，美国人常常不是中国人的对手，可是一到实践领域，搞点研究性题目，中国学生往往没有美国学生那么机灵，那么富有创造性。”我想，他的感受可能正是两种不同的基础教育体系所造成的人之间的差异。中国人太习惯于在一个划定的框子里去旅展拳脚了，一旦失去了常规的参照，对不少中国人来说感到的可能往往并不是自由，而是慌恐和茫然。我常常想到中国的小学教育，想到那些课堂上双手背后坐得笔直的孩子们，想到那些沉重的课程、繁多的作业、严格的考试……它让人感到一种神圣与威严的同时，也让人感到巨大的压抑和束缚，但是多少代人都顺从着它的意志，把它视为一种改变命运的出路。这是一种文化的延续，它或许有着自身的辉煌，但是面对需要每个人发挥创造力的信息社会，面对明天的世界，我们又该怎样审视这种孕育了我们自身的文明呢。</div>

							
							
							
							
