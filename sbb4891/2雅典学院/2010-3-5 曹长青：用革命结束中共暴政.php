<br />
说他不是被统治阶层的选项，因为现在是冷兵器VS拖拉机的年代。被统治阶层已经没有可能性组成有组织的暴力抵抗力量。第二个原因是被统治阶层已经分化成立动手的和动口的两个阶层。动口的无法用思想武装动手的。动手都是一种极端情况或者极端人格的应激反应。所以传统意义的革命已经消失了，没有必要再讨论。<br />
<br />
说他不是统治阶层的选项，是因为统治阶层从来没有放弃自己利益的可能性，而且中国没有西方那种一种超验的力量的存在。统治阶层会更加肆无忌惮，而被统治阶层也没有思想武器。统治阶层不会主动放弃的另外一个原因是统治阶层整体的利益诉求会超越道德诉求。从历史经验和统计学分析角度看，统治阶层内部也有少数良心的觉醒者，但是一定会被逆向淘汰出去，不会掌握真正的权力。<br />
<br />
说统治阶层和被统治阶层都无法选择革命，不表示革命不会爆发。革命的爆发是双方无法化解的矛盾的深入和激化，仿佛是一种强大的宿命式的漩涡。<br />
<br />
面对这个漩涡，我们有些事情可以做，第一，尽量避免这种漩涡的出现。在漩涡吞噬一切之前尽量消解他，这些人本身就是漩涡的牺牲和祭品，他们也许无法看到明天，比如林昭，有些可以看见，比如曼德拉。进入漩涡的都是理想主义的圣徒，自己跳进去值得我们永久纪念，而呼吁别人一起则至为可疑。所以他们最好说“我”；第二，尽可能削弱这种漩涡的强度。漩涡的强度更有赖于被统治阶层的政治成熟度。这里的被统治阶层不是所有的被压迫者，在英国的光荣革命中，他们是贵族，在美国，他们是殖民地的白人。更低一层的被统治阶层往往无法受惠于当时的革命。政治成熟的民族会以更小的代价逐步获得全民的解放，而政治不成熟的民族会付出更多鲜血的代价。当然，现在基本上没有更低一层的存在，对底层政治政治成熟度的培育更显重要。<br />
