													<h2>曹长青：用革命结束中共暴政</h2>
						
						
																											<div id="postmessage_502842" class="t_msgfont">(博讯北京时间2010年3月05日 转载)<br />
&nbsp;&nbsp;明年是辛亥革命一百周年，中国人关于走“改良”还是“革命”之争，也争了一个世纪。对于目前的中共专制，最近这些年来，主张改良、渐进改革的声音，在中国文化人中较占上风。“改良派”的主要理由是：其一，改良渐进可以不流血，代价小；其二，辛亥革命没有带来民主自由，而是军阀混战，最后共产党专制；其三，今天中共拥有庞大武力，人民没有推翻它的力量。<br />
<br />
但这次温和改良派向中共谏言被重判事件，再次引起改良和革命之争。“革命派”指出，面对这样的暴政，改良这条路是走不通的，不仅自取其辱，还误导民众，无 法创造推翻专制的契机。<br />
<br />
那么这两种意见，到底哪一种更有道理，更有可能解决中国的问题？一厢情愿的设想是没有意义的，只有从世界历史的经验，以及辛亥革命本身的教训中，或许才可能得到一些真正的启示。<br />
<br />
● 武装革命是“另一种途径”<br />
<br />
在人类历史上，一直有限制绝对权力的呼声，早期是用上帝来制约国王，第二阶段是强调自然法、普世价值，最后是今天的议会政治。近代自由主义主要发端于十八世纪，当时随着工业革命、中产阶级的兴起，人民要求限制君王权力，呼唤自由、平等。<br />
<br />
据美国政治学者华特金斯（Frederick Watkins）所著的《西方政治传统》，十八世纪时，从俄国的皇帝，到西班牙、奥地利的统治者，都感受到改革气息，因而做出政策调整和让步，走向开明专制。当时不少欧洲知识分子认为，“启蒙运动的理想，可以通过开明专制的措施得以实现。”<br />
<br />
但“第一个暗示人民必须选择另一途径来解决政治问题的事件是美国革命。”美国人民拿起了枪杆子，用武力革命的方式，推翻了英国殖民统治。美国的建国先贤们和当今中国文化人的想法不一样，他们坚信，面对暴政，人民有武装自卫的权利，有用革命推翻专制的权利。<br />
<br />
在欧洲，当时不少知识人对君王的 “开明专制”抱有幻想，但普通民众的“态度逐渐转变成批判式的，最后终于演变成革命的心态”。认识到“除了以革命解决之外，别无他法。”在美国革命的十多年之后，法国大革命就爆发，推翻了路易十六的专制王朝。<br />
<br />
●美、法革命为何结局不同？<br />
<br />
有些中国知识人用法国大革命后期出现罗伯斯庇尔的断头台（恐怖滥杀）来否定“革命”方式。但同样是革命，美国革命不是成功了吗？这里关键在于，推翻专制王朝之后，以什么样的理念和原则，建立一个新制度。<br />
<br />
对法国大革命有深入研究的法国作家雨果，在其《九三年》这本名著中，对这场革命持这样的看法：路易十六的王朝是腐败、专制的。拥戴国王的老伯爵，其庄园的“塔楼”代表君权、特权、神权。这块土地洒过太多的鲜血和眼泪，埋有太多的尸骨和罪恶，它孕育反抗和革命的种子；最后 “断头台”暴怒地站起来跟 “塔楼”对抗、复仇。雨果虽然非常严厉批判、否定罗伯斯庇尔的“革命”，甚至说“大革命就是断头台”，但他仍然认为，推翻路易十六专制的革命是必要的，只是大革命错在建立了更残暴的统治。《九三年》里的英雄是维护君主制的老伯爵，但雨果本人认同的是共和的理念；用他的话说，需要一种有人道主义精神的革命。<br />
<br />
同样是“革命”，为什么美国和法国的结果不同？就在于美国人实现了雨果的梦想，人道主义成为革命的核心价值。美国人革命的目标，是使人成为自由人，保护个人的权利。美国《独立宣言》把人的生命、自由、追求幸福的权利，提升到“是上帝给的、是天赋的”这样绝对的地位，以此来保证这些个人权利不被任何力量剥夺。《独立宣言》的精神，体现在11年后制定的《美国宪法》，其宗旨是“保护个人权利，限制政府权力”。<br />
<br />
哈耶克在《自由秩序原理》中，对美国先贤非常敬佩，他说，“美国人特别幸运，其它民族似乎都不及他们，因为在他们的领袖当中有不少是深刻的政治哲学家。”虽然独立战争时美国在各方面仍极为落后，“但在政治科学领域中，美国却处于第一流的地位。”美国的建国先贤们“不仅通晓古典传统，而且对英国思想家所提出的种种理念也了如指掌。”哈耶克所说的“英国思想家的理念”，就是指英国以“辉格党”为代表的古典自由主义者。所谓古典自由主义，按哈耶克的解释，就是把自由，而不是平等或民主，作为主要追求目标。当时美国的建国先贤，清晰、热情而坚定地信奉这种自由主义。哈耶克说，“当时对辉格诸原则的信奉，已达到这样一种程度，甚至连华盛顿的士兵都穿着具有辉格传统的‘蓝色加米黄色’的制服，一如英国议院中的foxites的颜色。”<br />
<br />
正是把个人自由作为根本，而不是平等或民主，所以杰佛逊起草的《独立宣言》，全篇没有提到“民主”，重点是强调个人权利。随后的《美国宪法》（译成中文一万多字）也通篇没有“民主”两字。这是美国先贤深思熟虑的结果，就是要防止以“民主的名义”，“人民的名义”，“多数的名义”，剥夺个人的权利。正是对“个人权利”这样极为重视并用法律严格保障，美国才没有出现法国大革命式的断头台和暴民政治。<br />
<br />
法国大革命虽然是受美国革命鼓舞而爆发，当时也制定了《权利法案》，想保护个人权利，但很快法国人就提出“人民主权”，一切权力属于人民。结果，今天罗伯斯庇尔用 “人民”名义，明天马拉和丹东用“人民”名义，剥夺了一个个具体个人的权利，最后他们自己也被“人民”送上断头台。哈耶克说，“导致整个结果的决定性因素，是法国大革命所创造的这样一种信念：既然所有的权力最终已被置于人民之手，那么一切用来制止滥用这种权力的保障措施，就变得不再有必要了。”“法国大革命从未达到美国革命的那种成就，即没有制定一部对立法权力施以限制的宪法。”《论美国的民主》作者托克维尔也说，法国大革命的根本问题是没有解决“行政机构的权力问题”，没有从根本上保障个人权利。而随后的拿破仑政权，更是强化政府权力，而“较少关注对个人自由的保障”。<br />
<br />
●辛亥革命错在哪里？<br />
<br />
中国的辛亥革命也有同样的问题。面对腐败、专制的清王朝，用革命推翻它是必要的。慈禧太后等顽固派拒绝改革，使康梁等保皇派的改良谏言没有实质效果；戊戌变法完全失败。当时只有革命，才能结束皇朝制度。这场革命的正义性、道义性、必要性，今天来回顾和评估，仍是必须肯定的。问题出在革命之后，国共两党都没有走美式的以保护个人权利为核心价值的建国之路，因为不仅在中国的政治领袖中，而且在几乎所有知识分子中，都没有人高举出“保护个人权利”的大旗，甚至对个人权利意识根本没有概念。在思想理论远没有到位的情况下，中国的革命最终走向独裁专制是必然的。<br />
<br />
辛亥革命之后八年，中国就爆发五四运动。我在“从否定五四运动开始”和“火烧赵家楼烧毁中国”这两篇文章中曾详述，五四运动怎样使中国走向法国大革命式的、以群体名义剥夺个人权利的专制之路。从五四运动的领导者（陈独秀，后成为中共总书记）、口号（反帝反封建，实为排外、煽动民族主义和摧毁伦理道德价值）、结果（中共兴起）这三方面来看，这是一场煽动集体主义，走向集权主义的运动。而“火烧赵家楼”则是典型的以爱国的名义剥夺个人权利（烧毁私宅，群殴嗜血）的暴民运动。毛泽东后来领导的湖南农民运动，文革红卫兵的打砸抢，和火烧赵家楼的本质是一样的。今天共产党仍以这种群体名义剥夺个人权利，其集权本质没有任何改变。<br />
<br />
哈耶克深为感慨美国人的幸运，因为他们的建国先贤是一批头脑清晰的政治哲学家。相比之下，中国当时的主要思想者，基本是集体主义者（而不是个人主义的提倡者）。鼓吹共产革命的陈独秀不必说（陈晚年肯定英美价值，但晚矣），连被称为自由主义先驱的胡适，当时去了趟苏联，回来就歌颂社会主义（实为群体主义）。研究这段历史的中国学者袁伟时曾说，当时中国的知识分子，无论国民党的，共产党的，还是民盟的，“共同理念是：私营经济和私营企业家是社会不公乃至罪恶的源泉”，几乎清一色主张国有化、国家主导的计划经济，这跟美国的个人权利至上完全不同（个人权利的最重要体现之一是个人财产权）。西方所以建立起市场经济，主要是强调和实践了个人私有财产神圣不可侵犯的理念原则。<br />
<br />
在五四和火烧赵家楼的集体主义意识形态下，共产党以群体的名义，人民的名义，夺取政权，建立暴政，专制至今。国民党虽有所不同，提出“军政，训政，宪政”三阶段，最终目标是宪法政治。但这个军政、训政要多少时间？没有明确的规定，结果三阶段说等于给了独裁者专权的借口。蒋介石在台湾一直“军政”到死，父子俩用军事戒严法统治了三十八年，连国民党自己通过的宪法都不实行。<br />
<br />
●用革命结束专制是必要的<br />
<br />
今天，回顾世界和中国历史，至少可以得出两点结论：<br />
<br />
第一，用革命的方式结束专制是必要的。美国革命当然是成功的样板，即使法国大革命，中国的辛亥革命，甚至俄国革命，都是专制王朝的腐败和残暴导致的必然结果。看过《日瓦戈医生》的人可能会记得那个俄国青年，他参加的和平示威，被沙皇的马队砍杀、镇压，无数人陈尸街头。他捂着手上的血说，“再不会有和平游行了。” 他藏起了枪，参加了革命。正如美国《独立宣言》所说的，“政府企图把人民置于暴政之下时，人民就有权、也有义务推翻这样的政府。”<br />
<br />
反对革命的中国知识人，总愿用英国光荣革命（1688年）为例，强调那场革命就没有流血，没有社会动荡，却使英国走向了议会政治。但很多人忽略了一个重要的前提：英国光荣革命绝不是改良派用下跪、谏言、恳求权力者的方式完成的，而恰恰是用武力进行的。是荷兰军队攻进英国，用枪杆子赶走了暴君。那场革命之所以没有流血，是因为国王詹姆士看到败局已定，仓皇逃走，等于“投降”；而不是他主动开恩、接受谏言，让步妥协的结果。而在这之前，詹姆士国王就曾对改革派进行了严酷镇压。所以英国的这场君主立宪变革，仍被称为“革命”！而赶走暴君之后，英国人首先做的是签订了《人身保护法案》，强调保护个人权利，限制统治者的权力（国王的权力转到国会）。所以中国人需要再清晰地明确一次：英国的“光荣革命”同样是靠武力解决了专制强权！<br />
<br />
●高举保护个人权利的大旗<br />
<br />
第二，用革命结束专制之后，关键是以什么原则和理念建立新制度，是保护个人权利，还是继续高举至今无数中国文化人推崇的群体、国家、民族利益的大旗？只有把保护个人权利放在高于一切群体、“人民”的利益之上，才可能建立一个真正的宪政共和国。<br />
<br />
今天，在中国民运组织、异议人士中，没有任何人主张结束中共专制之后，要建立另一个独裁制度。而且苏联及整个东欧共产国家垮台之后，全都建立了民主制度，所以没有理由和根据说，中国人在结束共产暴政之后，就会走跟俄国及东欧国家不同的道路。<br />
<br />
改良派可能会说，那怎么个革命法，可行的道路在哪里？道路在正确的思想精神指导下就会走出来。具体方式，没有谁可以准确制定和预测。但只要传播中共是邪恶，是狼，必须结束它，才有民主政治可言。只要这种理念深入人心，就会有推翻中共暴政的契机出现。而一味谏言请求，用多大的善意，也改变不了独裁者的本性，更不可能创造结束专制的契机。古语说，知己知彼，方能百战百胜。不把敌人当敌人，不知敌人本性，怎么有战胜敌人的可能？那“己”的力量在哪里？在每一个追求自由的人心中！天下从来没有任何力量，超过人心中追求自由的力量大！这个力量，使全世界多数国家成为了民主国家，中国人绝不会例外！<br />
<br />
当年萨哈罗夫、索尔仁尼琴们强调必须结束共产暴政时，完全不知道后来是叶利钦站在路障上，振臂高呼，最后有七十年历史的共产帝国，三天就垮台了。他们当年完全不可能预测是这种方式。当年如果有像中国改良派这样的人去质问他们，你给我说说，怎么结束共产党？他们也一定无法回答，但他们确信：共产党是邪恶，一切专制独裁都是绝不可忍受的邪恶，对邪恶不可再抱任何幻想，必须用一切手段结束它。坚定、清晰、不断地传播这个信息，让这个信息深入人心，就有推翻专制的契机、奇迹出现。结果，他们成功了。<br />
<br />
●别再梦想 “朝野良性互动”<br />
<br />
今天，对于中国知识分子来说，不用你“原创”，你只要学习美国先贤的思想精神，“copy”俄国以及东欧知识分子的经验，中国革命就有成功的可能。今天的问题是，很多人总是强调中国现实的特殊性，把眼光盯在统治者身上，幻想皇帝能接纳谏言、开明起来，而不是把眼光放到民众身上，靠民众觉醒后产生的力量。不少人一直期待“朝野良性互动”。问题是，在野，就有“上朝”的可能。但共产党承认你“在野”吗？它只知道要拼死保住自己的朝廷。<br />
<br />
思想是行动的前提和基础，在今天中国知识界那极少数要挑战专制者的人中，如果“谏言”“改良”的声音还是主流的话，那么共产专制在中国的结束就仍是遥遥无期。<br />
<br />
百年前的戊戌变法，以康梁逃亡国外，谭嗣同等六君子被杀害结束。百年后今天中国的现状，连戊戌变法前的中国都不如，岂止不如，几乎是没法比。戊戌变法之前，康有为、梁启超等人，可以在全国到处结社、出版刊物，领导思想风潮，宣传鼓动维新改革。而今天，在中国没有任何结社的可能，更没有任何出版自由。一个温和的谏言，都绝不可能在任何中国报刊发表出来；一个温和谏言的执笔者，都被投入监狱，中国人还要继续一厢情愿地期待共产党改革吗？难道现在还不到理直气壮地呼吁要用革命结束中共政权的时候吗？<br />
<br />
2010年3月2日于美国<br />
<br />
―― 转自《观察》</div>

							
							
							
							
