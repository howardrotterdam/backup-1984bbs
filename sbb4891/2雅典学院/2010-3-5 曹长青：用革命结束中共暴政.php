但是，由于热兵器的出现，尤其是信息和运输方式的现代化，所谓在薄弱地区首义的方式几无可能。<br />
<br />
如果是在中心地区，有足够多的民众支持，以肉身对峙刀枪，能否以道义感化暴力――暴力是通过非执政的人，即普通的士兵实施的，哗变或倒戈是有可能的。但是，二十年前有过一次相近的演习，结果是什么大家都知道。也许是事先的思想准备不够？<br />
<br />
如果有足够的民众认同和心理准备，以暴力或准暴力（前苏联并没有真正实施暴力）结束暴政，个人认为是不坏的期望。建立新暴政的可能很低，历史从来不会简单重复。有过如此经历的民众已经有过深刻的教训和理念上的提升，我相信，没有人能在之后的中国建立毛式政权。当然，初建的制度一定不完善，但一定不会是简单重复。实现足够满意，或说可以与当今民主政权相比肩的制度，个人很悲观地认为：需要数十年之久。<br />
<br />
问题是，当前的民众在理念和心理上，有没有足够的准备？当下的环境有没有对广大民众进行启蒙和宣传教育的可能，或说能有什么样的进度和已经达到了什么样的效果？<br />
<br />
也许，所谓“天理不容”，所谓“天灭”，所谓“是无天理”……是在等待“天”，而不是在等待“民”。多行不义必自毙。由于权势集团自身的原因，政权弱化，比如政令不出中南海；由于环境资源的恶化，民众难以为生；由于经济自身或掠夺过度，爆发大范围的抗暴（并不是主动的暴力推翻），无政治诉求的抗暴；再或由于大范围的瘟疫等等……压倒骆驼的最后那根草是难以预料的。<br />
<br />
即便是出现如华盛顿或叶利钦等精英，以暴力或准暴力实现了变革，接下来需要的，仍是民众的理念有所提升。<br />
<br />
所以，我们现在能做的，就是在各种场合，以各种方式，进行“民主预演”，包括在网络论坛上表达和争论。动辄将不同观点一棒子打翻，或以恶意相向，均为需要每个人从自身清除掉的“狼奶遗存”。<br />
<br />
顺便说，维权，也是一种非常好的启蒙和认清对方的形式。所谓“每个个体的权益维到了，整个民众的权益也就维到了”，是一种善良的愿望，制度绝不会由此而改变。我支持维权，但我绝不认为维权能改变制度。<br />
冯正虎这次维权，整个过程很漂亮；他在维权上做的事情，从看到的和他自我介绍的，个人很钦佩。但是，他在壹报采访中所宣扬的，自身逻辑矛盾，客观上具很大迷惑性。是策略性调侃还是善良性糊涂？还是其他？</div>

							
							
							
							
