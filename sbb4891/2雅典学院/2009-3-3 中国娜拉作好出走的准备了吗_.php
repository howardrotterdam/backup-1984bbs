&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;1.共产党内部并非铁板一块,什么样人都有. <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;2.民主这东西逐渐深入人心是无法阻挡的历史潮流.&nbsp; &nbsp;<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;我便开玩笑似的跟他说,也许有朝一日中共内部分裂了,而像你这样的一群人组成了一个新党,成为中国实现真正民主时的第一个政党.他哈哈大笑着说,到了那一天,我就邀请你加入这个政党.我虽然脸上挂着笑容,但心里却冒出另一个问题来:中国娜拉已经作好出走的的准备了吗? <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;今日之中国,但凡有识之士都认识到只有学习西方成熟,科学的政治制度才能让中国走上真正地强盛之路----这是摆脱缠绕中国几千年的治乱循环的历史怪圈的唯一方法.即使在体制内,也不乏其人.他们开始崇尚西方的民主,认为只有民主才能救中国.而这,也正是我所担心的地方.因为,&quot;民主&quot;与&quot;多数人的暴政&quot;只在一线之间.倘若中国娜拉的出走告别了&quot;少数人的专制&quot;而迎来了&quot;多数人的暴政&quot;,那这样的出走有何意义?它只是将中国的历史改写成&quot;由一个人的独裁,变为少数人的专制,再变为多数人的暴政&quot;.这样的悲惨局面是与有识之士的良好愿望完全相悖的.哈耶克说,通往地狱的道路,往往是由人们的良好愿望铺就的.因此我希望中国娜拉在出走之前,应该先冷静下来,好好思考一下为什么出走以及出走以后应该怎么办. <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;在我看来,民主的确是中国最需要的东西,但它只能算是一个必要条件而非充要条件.因为,今日之中国需要的东西太多了.比如自由,比如人权,比如宪政...这些东西与民主无关,并不是说只要有了民主,这些东西就到手了.因此,我认为我们更应该追求比民主更深层更广阔的东西,那就是自由和宪政.只有以捍卫自由为精神之基础,以实现宪政为国家之首要,我们才能在这样的基础上现实真正地民主----用我们手中的选票来选出执政党----而不是用&quot;多数人的意志&quot;来侵犯少数人的权利----更不是用&quot;民众雪亮的眼睛和心中的正义&quot;来审判&quot;全民公敌&quot;. <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;遥想当年美国娜拉从英国出走时,她没有想过出走以后应该怎么办,她没有哲学,没有理论,没有主义,甚至没有文化.有的,只是一种纯朴而单纯的念头----我要自由.这就是她揭竿而起时的那根竿上唯一的一面旗帜.她的出走更像是一个莽撞的村妇,而不是一个经过深思熟虑的知性女子.她唯一能向世人告之的,便是&lt;独立宣言&gt;里那句&quot;人人生而平等,有生命权,自由权,追求幸福的权利&quot;.而这,却恰恰是人类最本质的向往与诉求.正是以这个信念为基础,美国先贤们才制定出了一部伟大的宪法和一部更伟大的权利法案,保障每个人的自由才得以逐步实现.因此,今日之中国最大的政治诉求,应该是对自由的诉求,对宪政的诉求.而民主,则是这一切都实现以后的水道渠成的事情了.中国娜拉切不可为了出走而出走,那&quot;砰&quot;的一声关门声固然让人激动,但激动之后呢?如果只是以出走为目的,那么让人激动的&quot;砰&quot;声之后必然陷入迷茫.中国娜拉切不可为了民主而民主,因为民主并不能天然的保障我们每一个个体的自由.我们必须时刻牢记:世界上任何一种意识形态的政府,都离不开人权的保障和法律的约束,这是任何一个政府赖以合法存在的唯一形式.我认为,今日之中国既需要法国革命时期的激情,还需要英国几百年来的保守,更需要美国建国时的妥协.唯有激情,才能让我们充满期待和信心的去追求自由；唯有保守,才能让我们在群情激愤时保持理性的思考；唯有妥协,才能让我们在各个利益集团的恩怨情仇之间找到最好的结果. <br />
