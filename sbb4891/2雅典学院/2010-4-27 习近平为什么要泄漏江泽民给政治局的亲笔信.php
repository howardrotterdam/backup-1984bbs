																											<div id="postmessage_586620" class="t_msgfont">习近平为什么要泄漏江泽民给政治局的亲笔信？/博讯独家<br />
(博讯北京时间2010年3月28日 首发 - 支持此文作者/记者)<br />
博讯记者27日北京消息，一位中共政治局常委的子女向博讯记者透露，外界传的60周年大庆后江泽民写信给胡锦涛的事不但确有其事，而且，内情并不简单。在北京参加完六十周年大庆后，回到上海的江泽民越想越不对劲，最后提笔亲自写了一封给政治局的信。在信中，江泽民认为要对建国六十周年分前后两个三十年的分割，不能混为一谈，更不能借后三十年经济建设取得的成绩而否定前三十年的失误。江泽民还在信中对高举毛泽东像的方阵提出了自己的看法，并对苏联继赫鲁晓夫后的改革给予前所未有的肯定。<br />
<br />
政治会议上宣读这封信的时候，胡锦涛当场表态，要把这封信发到下面，至于下面什么层级，当时有分歧，是发到省部级，还是县团级，最后多数常委认为，修改语句后，或者使用摘要的方式，发文发到县团级。可是，会议决定后一天不到，胡锦涛改变了主意，主张不下发这封信，到此为止，并立即收回所有信件复印件，不得外泄。可是，这一决定并不是正式作出的，虽然政治委员们觉悟都很高，立即照办。可还有一位利用口头通知的空隙与不正规性，钻了空子，把这封信的内容以正规的方式泄漏出来――那就是习近平。 (博讯 boxun.com) <br />
据博讯记者确切的消息称，在接到不扩散江泽民信件时，习近平正好要到党校讲话（副省级干部培训班），他在一百多位高级学员中，原封不动地传达了江泽民的来信，虽然宣读完后强调要“保密”，但从政治局层级一下子扩散到党校学员（副部级与个别即将升幅部级的局级干部），这“密”自然是无法保的，据说，这正是习近平的原意。(boxun)<br />
<br />
习近平为什么要急急忙忙泄漏江泽民的来信，这位常委的子女解释，政治风向变了，谁都不想被认为是站在保守与阻碍政治变革的一边，更不愿意与前三十年沾边。可是，这些年，不知道是有意的还是无意的，总有人释放了大量的消息，把江泽民归类为阻碍政治变革，制约“胡温新政”的太上皇与保守势力。江泽民等人早就很不满了，这次江泽民利用给政治局写信的机会，其实是向外界所有的人释放一个信号：江泽民才代表开明的改革派，是隔代指定的胡锦涛制约了江泽民的改革，而不是下台后的江泽民制约了台上的胡锦涛。胡锦涛如果想干事，完全可以放开手脚，但他却只想回到毛泽东时代，甚至怀念北朝鲜。<br />
<br />
习近平一直被认为是江泽民隔代指定的接班人，也被认为是代表太子党和利益集团的代理人，据说，正是因为这一点，让他在各种知识分子中的名声并不好。习近平利用泄漏江泽民来信的方式，向党内高级领导人表明，真正阻碍政治体制改革，不想往前走，反而想回到文革，抬出毛泽东像的是胡锦涛，而不是江泽民与习近平。<br />
<br />
