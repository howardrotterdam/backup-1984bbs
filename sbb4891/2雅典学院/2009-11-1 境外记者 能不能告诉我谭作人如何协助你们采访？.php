自由亚洲电台报道――<br />
<br />
广州中山大学教授艾晓明早前被限制入境香港后，日前因私访问香港，成功到香港。艾晓明也计划顺便在香港访问一些记者，他们中有曾在四川大地震期间，采访过被当局控告涉嫌煽动颠覆国家政权的四川作家谭作人，也有采访过谭作人一审开庭情况的人，但均遭到婉拒。<br />
 <br />
以下是自由亚洲电台驻香港特约记者心语的采访报导<br />
 <br />
本台之前曾报导广州中山大学教授、妇女和公共问题学者艾晓明用了一年多时间拍摄《我们的娃娃》，用镜头纪录地震后灾民的心声及他们所面临的困境，早前被控颠覆国家政权的四川作家谭作人也曾参与协助拍摄此片。艾晓明十月中原计划到香港参加第七届香港社会运动电影节，并播放《我们的娃娃》，而后却在深圳机场被劝阻，并被带返中山大学。艾晓明一周之后因私访友，这次顺利抵港。星期天已回到广州的她告诉本台记者，她星期四下午到香港，除了拜访老朋友外，也有心想访问在四川大地震期间曾采访过谭作人的香港记者：“他的起诉书上写，2008年 512地震后，被告谭作人多次接受境外媒体采访。发表了大量诋毁我党及政府形象的言论，这里的境外媒体包括香港媒体；有记者曾和谭作人一起做过采访，我希望他们能够谈一谈当时的情形。我不需要他们为一些政治敏感问题表态，也不需要他们为此做什么结论。”<br />
 <br />
艾晓明表示，她尝试联络了香港的几位记者却都遭拒绝后，在香港只短短停留两天便于星期六下午回广州：“记者来采访我时，我是接受的，但等到我想采访记者时，就被拒绝了，我觉得这不平等。对待谭作人此案我也这样的想法，当然我相信我的观点会被媒体记者质疑，他们说：记者不能接受采访，他们的上司也不允许他们接受采访。但我是从一个公民，一个独立媒体工作者的角度，我想，许多媒体采访我，我的校长也会不同意，我的单位领导也会不同意。同样地，境外记者去采访很多大陆的人，他们的领导也都不同意，可是，如果我们所有的人都不说话，我们的社会如何去实现这知情权？记者的节目又如何完成？他们的上司及公司又如何向观众交待呢？” <br />
 <br />
谭作人的法律辩护书上提到，5．12地震发生后，被告谭作人多次接受境内外媒体采访，也多次为他们采访调查当向导，如新华社、《t望东方周刊》、《第一财经日报》、《人与生物圈》等杂志，及香港政府所属的香港电台。无论接受境内或境外媒体采访，谭作人的言论都是始终如一的。而控方却独独强调“谭作人多次接受境外媒体采访，发表了大量严重诋毁党和政府形象的言论” 。艾晓明认为，采访过谭作人的境外记者应该为他作证。<br />
 <br />
曾在香港电台任职的现任香港记协主席麦燕庭对此表示：“我问过他(采访过谭作人的境外记者)，他后来去问，也曾经问过他的上司，但是艾晓明已经走了，在时间上根本来不及。 因为他是公务员，要经过上司批准。而上司的批准是要有时间的。我觉得一个基本的权利就是每个人都有接受采访和拒绝采访的权利。谭作人可以，其他人都可以。”</div>

							
							
							
							
