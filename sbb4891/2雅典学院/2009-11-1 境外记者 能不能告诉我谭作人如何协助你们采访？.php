																											<div id="postmessage_310493" class="t_msgfont">故事与极权主义<br />
作者：哈维尔<br />
<br />
<br />
　　　　　　　　　　　 ［捷］哈维尔　崔卫平译 <br />
<br />
　　我有一个患严重气喘病的朋友因为政治上的原因被判刑，在监狱里过了好几年。在那里，他受害弥深。因为他的狱友吸烟而他几乎不能呼吸。他换一个无吸烟者牢房的要求都没有人理睬。他的健康，甚而他的生命，受到很大威胁。一个美国妇女知道了这件事并想帮助他。她打电话给一个熟人，一家重要的美国日报的编辑，问他是否可以写点什么。“那人死时给我打个电话”，那位编辑回答。 <br />
<br />
　　这是一件令人震惊的事情，但在某些方面是可以理解的。报纸需要一个故事。气喘病不是一个故事。死亡可以使它变成一个故事。 <br />
<br />
　　在布拉格，只有一家长期委派的西方通讯站。在黎巴嫩，一个远比捷克斯洛伐克小的国家，有几百个以上。也许这是可以理解的，因为正如他们所言，“这儿什么事也没有”。比较起来，黎巴嫩充满了故事。它是一个谋杀、战争、死亡之地。像人类所能记忆的那样长久，死亡从来就是每一个真正的故事的凝聚的焦点。 <br />
<br />
　　我们的情况像我的朋友：我们不值得注意因为我们没有故事，也没有死亡。我们仅仅有气喘病。为什么必须有人对我们的咳嗽声感兴趣？ <br />
<br />
　　人们也不可能永远写呼吸是多么困难这件事。 <br />
<br />
　　我并不为这样的事情烦恼――在这里恐怖分子是不是逍遥法外，或者在上层有没有重大的有关腐败的丑闻，或有没有暴力示威或罢工。 <br />
<br />
　　令我感到烦恼的是另外一些事情：这种有新闻价值的故事令人奇怪的缺席并不是社会和谐的一种表达，而是一个危险的和极端的过程的外在结果：所有故事的消灭。几乎每天我都为这种社会沉寂其意义暧昧不清而感到震惊，这是在极权主义制度与生活本身之间看不见的战争的可见的表达。 <br />
<br />
　　认为捷克已经从战争和谋杀中摆脱出来这不是实情。战争和杀戮采取了不同的形式：它们从光天化日下无所遁形的公开事件转变成朦胧不可见的内部的毁灭。人们在故事中读到的那种纯粹的“典型”的死亡（尽管它所包含的恐怖，却仍能不可思议地透露出人类生活的意义［此处有语病］），在这里已经由另一种死亡取代：慢性的，遮遮掩掩的、不流血的，不再是纯粹的，然而是没有行动，没有故事，没有时间的死亡，集体的死亡，或者更确切地说，麻木不仁的，社会和历史虚无化的过程。这种虚无化如此取消了灭亡，同样如此取消了生命：个人的生命变成一个大机器中功能单调、整齐划一的组成部分，他的死亡仅仅是意味着卸除了他的使命。 <br />
<br />
　　所有这些迹象表明，这种情况是发达的、稳固的极权主义制度的内在表达，直接从其本质中生长出来。 <br />
<br />
　　来自西方的参观者经常惊讶地发现对捷克人来说，切尔诺贝利核辐射和艾滋病并不是一种恐怖的来源，而更像是一个笑话的主题。 <br />
<br />
　　我必须承认这并不令我惊奇。因为极权主义的虚无化完全是无形的，比起艾滋病病毒和切尔诺贝利核辐射它更看不见，然而更为当下的，更危险，换句话说，它比艾滋病或核辐射更内在，更紧迫地触及我们每个人，因为我们每天都以个人经验去了解它而不是从报纸和电视上了解。因此，那些比较起来不那么具有恐怖意味，不那么阴险、不那么内在的威胁就被驱逐到背景中去并遭到忽视，是不足为奇的。 <br />
<br />
　　这种无形的胜利还有另外一个原因，故事的消失意味着人类认识和自我认识一个基本手段的消失，极权主义的虚无化否定人们从外面观察和理解其过程的可能性。这里只有两种选择；要么你直接经验它，要么你对其一无所知。某种恐吓不允许对其作公开查询。 <br />
<br />
　　外国旅游者可能会形成这种合情合理的印象：捷克斯洛伐克比瑞士更穷、更枯燥，报界对在这里关掉他们的办事处有一个合情合理的理由：他们怎么能指望没有事情可报道。</div>

							
							
							
							
