																											<div id="postmessage_311221" class="t_msgfont">麦燕庭的回应真是让人心寒。<br />
<br />
“每个人都有接受采访和拒绝采访的权利。谭作人可以，其他人都可以”，这句话，基本原则是不错的，但是放在谭作人的案子上，或者中国的语境下，是没有道德承担的：因为对于普通的中国公民而言，只有拒绝外媒采访的权利，而万万没有接受外媒采访的权利。香港媒体工作者往返内地，对这一点难道不心知肚明吗？既然谭作人接受香港媒体采访，根本不是一种权利或者自由，而是一种良心和道义的选择，那么，麦燕庭的此番说辞，就是站不住脚的。<br />
<br />
当然，对于记者个人而言，出于压力，或者内心的懦弱，拒绝采访，可能在一般人看来，是可以理解的。但是，如果不批评媒体”逐利“的本质，不挑明外媒在报道中国问题时，既享受了新闻自由的”治外法权“，又享受了报道”极权风景“所带来的”新闻红利“，那么，我们对外媒记者的人格和道德要求，未免低而又低。<br />
<br />
正如前一段麦燕庭代表香港记协，抗议新疆武警殴打香港记者。面对新疆新闻官员”采访者没有记者证“的回应，我在电视上看到香港记者的怒吼，“没有记者证就可以打人吗？”我们看到的是基于“不可以随便打人”的简单的做人道理，而不是“没有记者证”的所谓政策和规定。<br />
<br />
正如闾丘露薇对香港记者新疆被打事件发表不同意见，认为“记者不应该成为新闻报道的焦点”；有座谈会现场的香港记者基于义愤摔门而出。有不同的观点是正常的，但面对所谓的职业条规与具体情境下人的道德取舍的矛盾，什么是良心选择，什么是逃避责任，无论是以记者身份还是公民身份出现，在采访者和受访者之间应该有基本的共识和判断。<br />
<br />
因此，麦燕庭对于香港电台记者拒绝采访和为谭作人作证的回应，完全是双重标准：香港记者被打，你可以谴责可以抗议，谭作人为你的采访而坐牢，为什么你却要强调“不作证”是一种权利？<br />
<br />
不过，这件事也让我们看到公民记者与独立记录的重要性，体制内的媒体从业者与体制之间，一方面，体制给个人施加压力；另一方面，个人也完全可以用体制和行规作为幌子，逃避个人的道德责任。<br />
<br />
因此，我们一定要支持作为个体的公民记者，一定要支持独立记录；不是支持具体的某个人，而是支持一种基于人与人之间的道义诉求、而不是基于一种逐利本质的媒体工作。<br />
