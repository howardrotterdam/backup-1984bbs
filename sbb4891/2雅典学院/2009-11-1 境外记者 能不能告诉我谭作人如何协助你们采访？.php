													<h2>境外记者 能不能告诉我谭作人如何协助你们采访？</h2>
						
						
																											<div id="postmessage_309200" class="t_msgfont">境外记者 能不能告诉我谭作人如何协助你们采访？<br />
 <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;――艾晓明谈周末访问香港记者被拒<br />
 <br />
&nbsp; &nbsp; 我这次去香港，没有任何放映活动，只是看退休的老朋友。我顺利去了香港，两天就回来了。你说我去采访记者，是的，不过和乌鲁木齐没有任何关系；我关心不了那么多。我关心的是谭作人，因为一审结束后，很快他面临判决。我想请教在地震时期采访过谭作人的香港记者，原因是起诉书上有这么一句话：“2008年‘5&#8226;12’地震发生后，被告人谭作人多次接受境外媒体采访，发表了大量严重诋毁我党和政府形象的言论。2009年3月27日，被告人谭作人被公安机关挡获归案”。（顺便请教起诉书作者：“挡获”什么意思？我怎么只听说过“抓获”？！）<br />
&nbsp; &nbsp; 我见到了采访过谭作人的香港节目制作人，他是优秀的编导，有过很多作品获奖。不过，他没有接受我的采访。<br />
&nbsp; &nbsp; 我承认，人们有自由选择接受、或者不接受一个体制外的、独立制片人――我的采访，我完全尊重这个理由。但是我没有预料到这位朋友不接受，因为，我接受过他的采访；我想当然地认为，作为媒体人，他理解我们彼此的工作有多么重要，我们多么需要共同努力来追寻真相。<br />
写到这里，我还要强调，他并不反对我的这些想法；而他的婉拒，则使我想到了其他一些问题。我下面写的，就是我如何不理解记者不接受采访的依据：公司不同意、会对谭作人案造成负面影响、因为我们还要继续去大陆采访，接受你的采访，会使我们在大陆的工作更困难；更进一步，还有其他记者的补充说明：记者（作为不带偏向的、媒体生产者）不接受采访。<br />
&nbsp; &nbsp; 首先，我要说；我理解在大陆做采访的境外媒体工作者的困难，特别是报道问题新闻、有争议的事件时。但是，谈到这种困难和风险，我要补充的是，媒体工作者/贵公司老板，是否把被采访者要承担的风险考虑在内了？<br />
&nbsp; &nbsp; 举一个业界都知道的例子，当年公民吕邦列协助英国《卫报》记者采访太石村，被拖下车打得晕死。该记者后来写的报道说明了他自己被吓坏的程度，由此可见针对采访协助者的暴力之强烈。换句话说，如果境外媒体在采访某些问题时，得不到内地公民的帮助，他们基本上一事无成，至少难以作出深度报道；除非把自己的想象加进去。那么，对于这种一时的、参与了新闻作品制作过程的协助者，你媒体公司要不要承担伦理责任？据我所知，英国《卫报》机构是去到吕邦列养伤的家乡，支付了有关身体检查的费用的。<br />
&nbsp; &nbsp; 观察谭作人一案，也遇到同样情况。阅读四川成都市人民检察院对谭作人的起诉书可以看到：谭作人之罪并非因为他调查地震灾害，而是他就地震问题接受了境外媒体的采访。从起诉书措词我们看到的是“多次”接受，从浦志强、夏霖等辩护律师的辩护词里我们看到，法庭上提到的只有一家境外媒体：<br />
 <br />
&nbsp; &nbsp; 三、关于控方指控的谭作人发表的5.12地震有关言论的问题法庭调查表明：2008年5．12地震发生后，被告谭作人多次接受境内外媒体采访，也多次为他们采访调查当向导，如新华社、《t望东方周刊》、《第一财经日报》、《人与生物圈》等杂志，及香港政府所属的香港电台。<br />
 <br />
&nbsp; &nbsp; 那么，作为一个重要的、有广泛影响的案例，一个问题摆在中国内地公民面前，也同时摆在了境外媒体面前：当你去采访大陆公民时，你对他要承担的政治风险，要不要事先买类似旅游意外伤害的保险？要不要支付赔偿金？我这个推理实在有点荒唐，一个不需要思索的答案是：不要。你接受采访，你言责自负。但是，我认为媒体需要面对的问题是：你为什么要采访谭作人？你为什么要发表他严重诋毁党和政府的话（你为什么要害他）？假如他没有严重诋毁，那么他要被当作“严重诋毁”治罪了，你这个境外媒体是不是认同这个罪？假如不认同，你何以能够脱罪？“采访”、且让谭作人做过采访“向导”（律师辩护词语）的人，是否可以接受我的采访，说说当时的情形是怎样的？<br />
&nbsp; &nbsp; 说到这里，我需要表明一下个人态度：假如有人因为接受我的纪录片采访被治罪，我首先得承担责任。因为，如果他不接受我的采访，就没有机会犯罪。那么剩下的事情是，我要不要承担这个罪责？如果我认为这个不是罪，我只有一条路，就是讲出我的看法；讲出我的看法，也许不能救出某一个人，但是，一个法治社会可以有其他救济途径，我的证言，说清了我的责任。假如我不讲，原本是我们共同的罪，就变得要让被我采访的某一个人来承担。<br />
&nbsp; &nbsp; 现在的情况是，谭作人接受采访了，境外报道了。谭作人被抓了，境外又报道了；谭作人被审判，我相信，境外还会继续报道，甚至大热一场。谭作人成为消息热点，但他当初是如何接受采访/协助采访的（如何犯罪以及协助犯罪的）？采访过/被他协助过/引导过的人沉默了。<br />
在我眼里，这里的理由（记者不接受采访、老板不同意采访、我们还要继续采访……），构成了一道消费链，它们是我以前不曾思考的，当然也使我第一次开始反思境外媒体作为信息生产者的另一面：为了采访地震去采访谭作人、为了继续采访谭作人被审判而隐身沉默；我觉得就像一部有关越王勾践的戏剧里西施的台词：你把我送到吴国是为了打败吴国，你打败了吴国是为了把我接回来。我看到谭作人变成一道菜、变成抢眼新闻的热点，作为肉体的谭作人，不见了。<br />
不仅如此，我们所有这些曾经接受过境外媒体采访的人，还将面临谭作人同样的处境：今天我接受你的采访，明天我因此被治罪，后天你接着再采访我如何被治罪的……谭作人/我都是新闻消费产业上的一道原料、被利用的工具、境外观众消费的极权风景……如果我们只是这样的玩偶，还有必要接受境外媒体采访吗？<br />
&nbsp; &nbsp; 剩下的一些问题，我还没有机会问，在境外媒体之外，有境内媒体；在主流媒体之外，有边缘媒体；在媒体机构之外，有独立制片人。这些不同权力关系上的媒体人，能否彼此采访？是不是我作为独立公民，只能被动地接受采访，没有权力和渠道采访主流/机制内的媒体人？简言之，我再重复一遍：如果你不接受我的采访，我为什么要接受你的采访？你可以说，我不代表公司，所以我不能接受采访；但是，你代表个人的时候，能不能接受采访？你八小时之外，能不能接受采访？你采访谭作人的时候，是不是公司老板把谭作人的名字放在你的采访名录上、指定你去采访的呢？采访谭作人，和节目制作人的个人选择以及公司认同，又有什么关系呢？<br />
&nbsp; &nbsp; 谭作人案提出一个重要问题，也将确立一些重要原则，必将对中国公民社会产生深远的影响。一个是六四能不能纪念？纪念六四就是犯罪吗？这个问题，不是本文的要点。本文涉及到的是起诉书中的第二个涉嫌犯罪事实：案犯与媒体的关系。对于相关罪责，我试图理解其要点在哪里：如果境外媒体是主要问题，那么不能接受境外媒体采访（否则要治罪）；如果地震是主要问题，那么不能谈地震（否则要治罪）；如果严重诋毁是主要问题，那么不能严重诋毁（否则要治罪）；如果党和政府是主要问题，那么不能提党和政府（否则要治罪）……<br />
&nbsp; &nbsp; 对于上述问题，我其实没有能力做深入的调研取证；我只是一个用影像讲故事的人。我只想想请当时和谭作人一起工作的人，讲讲采访的故事而已。我当然了解谭作人这方面的一贯作风，我认为他不会“严重诋毁”，但是他可能有所批评。如果他的意见是符合事实的，被贵公司节目编成了“严重诋毁”，那么责任不能由他负，而要由境外媒体制作人来负。不仅如此，而且，如果没有境外媒体采访，他没有犯罪机会；如果没有境外媒体播出，他也没有施加犯罪影响的机会。作为一个惯例，媒体人不能将“严重诋毁”当作专家证言来播出，如果你当他严重诋毁，那么是你这个媒体制作者犯了严重的判断错误，误导了香港观众，播出了他的严重诋毁。在这个问题上，无论如何，境外媒体无法脱“罪”。只有一条理由可以摆脱罪责：那就是回到事实的出发点――谭作人是如何与境外媒体人协助采访的、他又是如何接受采访的。搞清了这些事实，观众、法院，都可以独立判断。<br />
&nbsp; &nbsp; 最后，一个个人的理由：法庭调查点出了境外媒体的名字：香港电台。而当香港电台的编导前往采访时，给过我一个电话，问我在四川有没有朋友可以协助。我提供了成都读书会朋友的名字，他们就是这样认识了谭作人，谭作人等一批志愿者协助了他们的采访。当时地震刚刚发生，我身不能救人，心向往灾区，自以为责无旁贷地要为香港媒体界朋友提供线索，使他们顺利完成采访。他们的节目播出了，在香港必定产生了积极影响，大量来自香港的捐款源源涌向四川。而当时，我、谭作人，我们都没有想到，会有因此被治罪的一天。</div>

							
							
							
							
