													<h2>余杰：温家宝迫害袁腾飞就是纵容毛派邪恶势力</h2>
						
						
																											<div id="postmessage_626646" class="t_msgfont">胡温当政以来，以民粹主义和民族主义凝聚统治合法性，并不断为暴君毛泽东招魂。中共建政六十周年庆典上，甚至排出了一个“毛泽东思想万岁”的宏大方阵，让人疑惑这究竟是在北京，还是在平壤。洞悉了胡温的此种心思，封冻二十多年的毛派人马纷纷活络起来，到处煽风点火，就连毛的弱智孙子毛新宇亦摆出将军派头来，只差就要问鼎中枢、讨伐四方了。毛之阴魂不散，毛派之沉渣泛起，得到了胡温的默许甚至鼓励。于是，有正义感的中国人不得不挺身而出，其中发出反毛最强音的，是作为历史教师的袁腾飞。<br />
<br />
温家宝企图让袁腾飞消音<br />
<br />
&nbsp; &nbsp;袁腾飞登陆央视《百家讲坛》而一举成名，本来在该栏目上露脸的人物大都是无耻的御用文人，偏偏有袁腾飞成为一个漏网之鱼。近日，袁腾飞的非毛言论在网络上广为流传，他说：“毛别说在中国历史上，在世界历史上都是少见的暴君独裁者。”他说：“哪个读过书的人，如果认为他伟大，那你的书就读到狗肚子里去了。”他认为毛比历史上所有的暴君都更坏：“毛已经不是皇上的问题了，他变成了教主了。所有的独裁者都是心里不健康的，而且都是弱智，没有一个独裁者能做出合理的判断来，二十世纪三大暴君：希特勒、斯大林、毛，全是这个德性，是吧？三大法西斯恶魔都是这个德性。”他还这样评价毛之纪念堂：“毛纪念堂不是不可以去，你可以去，但是你要记住那是什么，那是靖国神社，里面供奉的是一个双手沾满人民鲜血的刽子手。”他对中共的政治图腾天安门广场亦如此评述：“天安门广场我是从来不去，我开车从那里经过，我都鸡皮疙瘩，四千多万冤魂索命啊，太可怕了，那个地方就应该改成大屠杀纪念馆，纪念碑也是，把那碑文铲了，改为：死于一九四九到一九七八政治恐怖的人们永垂不朽，应该有这个殉难者纪念碑。”<br />
&nbsp; &nbsp;袁腾飞因为这些言论引发毛派分子的辱骂，甚至还有人对他发出追杀令，如同当年霍梅尼下令刺杀《撒旦的诗篇》的作者拉什迪一样。不过，如今中国的毛派分子都是“语言上的巨人，行动上的矮子”，当初伊拉克战争爆发的时候，他们虽然信誓旦旦地说要组织志愿军去跟伊拉克人民并肩作战，但一直到萨达姆在地洞中束手就擒并被送上绞刑架，他们当中也没有一个人真正成行。所以，毛派虚张声势的恐吓，最多只是他们自己的精神胜利法罢了。今天的中国，虽然天安门城楼上依然悬挂着毛像，人民币上面依然印刷着毛像，但毛泽东思想对大部分民众的精神控制的局面已是明日黄花。中国知识界残存了一批“新左派”，如汪晖、王绍光、崔之元、杨帆、巩鲜田、张承志、韩德强、王小东、张广天、黄纪苏、旷新年、孔庆东、摩罗之流，声称他们如何崇拜毛泽东、如何怀念文革。但这仅仅是他们的生存策略，他们或者游学西方、享受丰厚的待遇；或者在中国的名校占据显赫位置，时时在电视上高谈阔论。另一方面，他们比大多数中国民众的生活更加资本主义化，他们中的许多人拥有豪宅和名车，也从不拒绝使用“美帝国主义”发明的互联网、津津有味地观看好莱坞的电影并享用其他西方的科技及文化产品。<br />
&nbsp; &nbsp;袁腾飞不会被毛派的恐吓所吓到。比起毛派张牙舞爪的威胁来，当权者作何反应呢？对老毛颇为尊崇的温家宝们可坐不住了，他们遂动用体制的力量，一边将袁腾飞散播在互联网上的言论尽量予以删除，一边命令袁腾飞所在的单位对其“批评教育”。《南方都市报》报道，袁腾飞所在的学校的相关领导，对袁腾飞进行了“警诫谈话，批评教育”，并且“责令其作出深刻检查，深刻认识自己在网络视频中，言论的严重错误，并要求其今后停止一切在民办培训机构等其他部门的授课活动……要以积极正确的态度对待此事，要配合有关方面做好工作，力争将影响降到最低限度。”并责令他作出深刻检查。看来，如果袁腾飞仍然不就范的话，下一步是否会敲掉他的饭碗亦未可知也。在此意义上，温家宝迫害袁腾飞就是纵容毛派邪恶势力，就是表明温家宝的思想状态仍然停留在毛时代。所以，袁腾飞的言论自由就是我们每一个人的言论自由，我们跟袁腾飞站在一起，就是与人类的自由与尊严站在一起，就是不畏毛派和当权者的威逼利诱，就是义无反顾地说真话、守真理。<br />
<br />
<br />
尼泊尔毛派是真正的毛主义者<br />
<br />
&nbsp; &nbsp;毛尸的存在是中国人民之耻辱。温家宝与毛尸近在咫尺，却无动于衷，可见其久闻其臭而不觉臭也。袁腾飞说：“我相信在我有生之年，能够看到毛泽东的尸体被从那里请出去，这是我唯一的追求。尼泊尔不是共产党毛派掌权吗？问他要不要，要送给你，免费的，我们帮你运过去，连那个纪念堂整个都拆给你。”袁腾飞说得好，今天真正的毛主义的信仰者，并不在中国，而在尼泊尔、印度等亚洲国家的贫困山区。毛泽东的幽灵还在这个世界某些角落的上空游荡。尼泊尔的毛派反政府武装，堪称亚洲地区最残暴的恐怖组织之一。他们残忍地拷打并杀害许多被俘的警察和尼泊尔皇家军队的士兵，他们驱使孩子参与前线的战斗，“童子军”成为杀人机器。自从一九九六年毛派武装与政府军发生剧烈冲突以来，已经有四千七百多名尼泊尔人死于双方的战斗之中。<br />
&nbsp; &nbsp;有国际媒体将普拉昌达与当年柬埔寨杀人如麻的“红色高棉”领袖波尔布特相提并论。《时代周刊》记者在采访尼泊尔毛派领袖普拉昌达采访时，这个以“小毛泽东”自居的恐怖分子侃侃而谈：“马克思―列宁―毛泽东主义是一种无产阶级社会革命的集成的科学，是从震撼世界的群众斗争中发展而来的。作为一种科学，它要不断地、持续地发展。‘普拉昌达路线’是马克思―列宁－毛泽东主义根据尼泊尔的条件的运用，也是对它的一种丰富。”就像毛泽东在国庆典礼的程序中自行增添“毛泽东万岁”的口号一样，普拉昌达也毫不掩饰地提出了“普拉昌达主义”――所有的独裁者都是那么地相似。独裁者都喜欢用自己的名字来命名“主义”，并拒绝承认这是一种“个人崇拜”的表现。<br />
&nbsp; &nbsp;尼泊尔毛派将革命输出到全世界的雄心壮志，丝毫不亚于昔日的毛泽东。普拉昌达宣称：“我们的人民战争是崭新的二十一世纪的战争。我们不只是在和专制君主制斗争，我们还要和罪恶的帝国主义世界斗争，与伪善的所谓民主制度如超级大国美国斗争。”在世界共产主义运动日薄西山的今天，这样的宣言无异于蝼蚁撼树，这样的空中楼阁永远也建不起来。尼泊尔毛派接受议会制度，通过选举上台执政后不久，由于治国无方，又被赶下了台。普拉昌达不能接受失去权力的结局，又在尼泊尔煽风点火，策动恐怖主义活动，一个宁静的佛国，被他搞得血雨腥风、人人自危。<br />
&nbsp; &nbsp;毛泽东转世到了尼泊尔，真是尼泊尔人民的厄运。而中共长期暗地里支持尼泊尔毛派武装势力，并没有像习近平宣称的那样，已经停止“输出革命”了。虽然外界一直有传闻说中共偷偷支援尼泊尔毛派反政府武装，但中国外交部发言人多次予以否认。温家宝上台以来，在每年的人民代表大会上倡导财政公开，但他有没有胆量公开中共的外援细目呢？中共究竟有没有将纳税人的血汗钱送给尼泊尔毛派搞武装斗争？温家宝敢不敢说一句良心话？温家宝敢不敢对此种肮脏的外交传统作出忏悔和更正？<br />
&nbsp; &nbsp;有意思的是，尽管得到中共方面的输血，但尼泊尔的毛派从不承认他们是中共的“儿子党”，并对九十年代以来从以江泽民为核心到以胡锦涛为总书记的中共积极加入全球化体系的经济政策不以为然。所以，与其说尼泊尔共产党人认同今日与国际资本翩翩起舞的中共当局，不如说他们认同当年闭关锁国、斗志昂扬的老毛及其思想方式。而胡温则是心向往之，力不能及也。<br />
<br />
<br />
没有非毛的言论自由，而有颂毛的言论自由<br />
<br />
&nbsp; &nbsp;毛式共产主义是共产主义体系中最邪恶的变种和异形，无论是作为原创地的中国的毛主义，还是作为徒子徒孙的意大利“红色旅”、柬埔寨“红色高棉”、秘鲁“光辉道路”，以及尼泊尔毛派武装，其残忍与恐怖，倘若马克思复生，也会吓得魂飞魄散。二零零七年，布什总统在华盛顿亲自出席共产主义死难者纪念碑揭幕仪式时，发表演讲指出，残暴的共产主义思想危害世界各角落，美国首都却一直没有一座纪念共产主义受难者的纪念碑，实在说不过去。他对受难者家属锲而不舍地争取十多年，终于一偿宿愿，表示由衷敬佩。他指出：“共产主义不只夺走受难者的生命，他们还企图盗窃他们的人性，抹消他们的记忆。这座纪念碑的落成，就是要归还他们的人性，重建他们的记忆，他们将永远不会被忘记。”<br />
&nbsp; &nbsp;在这篇演说中，布什总统将共产主义和奉行伊斯兰原教旨主义的恐怖主义归为一类。这是一种崭新的视角：恐怖主义是一种近在眼前的威胁，而共产主义在苏联解体之后变成了一种潜在的威胁。北韩、古巴等强硬的共产党小国，如同虱子挑衅狮子，虽然让狮子不舒服，但还不足以危害全球和平；越南共产党的本质虽然没有改变，但其改革大幅推进，并频频向美国抛出橄榄枝；即便是目前最大的共产党国家的中共政权，在对西方的外交政策中，也以赚钱为最高目标，甚少鼓吹意识形态。于是，某些西方人士对共产主义失去了警惕，“九&#8226;一一”之后，西方将重心转移到反恐方面，而将反共政策暂时搁置。然而，反恐固然重要，反共亦不可缓行。共产主义的危害虽然不及冷战时代那么迫在眉睫，但其百足之虫、死而不僵，随时有死灰复燃的危险。<br />
&nbsp; &nbsp;温家宝的青年时代是在毛泽东的统治下度过的，有的人在苦难中反思和觉醒，有的人则在苦难中沉沦与愚蠢。在这一点上，胡温比江朱大踏步地倒退了。江时代的五十年大庆，尽管也是好大喜功、奢华虚荣，但江至少还没有祭出毛太祖的牌位来；而胡时代的六十年大庆，则是群魔乱舞，毛魂卷土重来，表明胡温对毛时代基本持正面的评价，企图利用毛的传统来整合中国社会。温家宝不容许袁腾飞自由地发表非毛言论，对一帮右派老人自己印刷一点揭露毛时代真相的回忆录并聚集在一起茶叙，亦要派出国保威胁恐吓；另一方面，毛派网站“乌有之乡”畅通无阻，毛太孙在政协会议上亦大放厥词。可见，今天的中国并不是完全没有言论自由，中国有一种畸形的言论自由，那就是：一些人有言论自由，而另一些人没有言论自由；一些言论可以自由发表，而另一些言论则不可以自由发表。<br />
&nbsp; &nbsp;考量温家宝打压袁腾飞的非毛言论，而纵容毛太孙及若干毛派的颂毛言论，就可以清晰地发现，温家宝的统治合法性仍然建立在尊毛之下，所谓“皮之不存，毛之焉附”，没有毛，哪有温？温家宝不会承认，抛弃毛主义才是其唯一的出路。他将抱着毛的牌位，与之一起跌入历史黑暗的深渊。</div>

							
							
							
							
