																											<div id="postmessage_181218" class="t_msgfont">“共产”两个汉字是日本人翻译来的<br />
日本人翻译的来源是Communism，或者communist这个词，他们在德语、法语里都有自己的counterpart词根都一样的，在英文里就是commune，德语是Kommune ，这个都是名词<br />
更早的拉丁文名词是communis、communia，动词应该是communicare<br />
<br />
意思是社群（公社、社团、社区）、交流（communication也从此而来），（另：辞典上还有说“领圣餐”之类的的意思，考虑到使徒行传里的基督教早期一些“共产公社”的行为，倒是颇有先见之明啊：）<br />
<br />
远的不论，在12世纪的时候，现在的法国就有了叫commune的社团组织，大家生活在一起，遵守共同制定的规则<br />
<br />
19世纪初的时候，“空想社会主义”（中学毕业的都有印象吧）著名的欧文，在美国还是什么地方，招了一帮人，建设反资本主义的实验性的“commune”，那就是真正意义上的“公有”的公社了，马克思体系的commune及其相关词汇的直接来源就在这里。同样，出名的巴黎公社，也是用的这个词。<br />
<br />
日本人翻译的时候，直接对准了马恩要求废除私有制，用共同使用全部生产工具并按共同协议来分配产品的意思，共有生产资料等等这些财产，这个就是“共产”来历<br />
<br />
我觉得翻译还是比较准确的<br />
