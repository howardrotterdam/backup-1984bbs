																											<div id="postmessage_494691" class="t_msgfont">这里盛行诛心之论：评喻高谴责艾未未<br />
<br />
<br />
俺对北京这些艺术家维权事件看过一些文字，比如写实先生贴出来的。了解不多，但看了喻高评艾未未，总是有些话不吐不快，说得合理不合理大家评评。<br />
这件事好像是这样的：喻高女士认为，艾未未等人未经维权艺术家团体的民主程序，擅自去长安街上游行，把事情闹大，把普通的反强拆维权弄成了政治事件，可能导致艺术家们与政府的矛盾，使已经看到的政府参与和平解决的苗头又将要失去，一句话，就是艾未未的行为“成事不足、败事有余”。<br />
俺觉得，喻高女士有权这样表达自己的看法。因为在这个世界上活着都不容易，没有谁有权力要求别人都去充当民主斗士，艺术家们维权，要的是个体的尊严和经济利益，把诉求框定在此范围，也是人之常情，无可厚非。喻高女士的担心，也是事关自己乃至许多艺术家身在事中的正常反应。<br />
但是俺要说的是，除此以外的反应，属于过度反应，甚至是一种病态反应。此话怎讲？因为喻女士在评价艾先生的动机时，直接肯定艾先生是在博取个人名声，捞取政治资本，甚至可能以牺牲艺术家们的维权利益为代价。<br />
这就明白无误地讲艾未未定位在了卑鄙政客的档次上。这就是诛心之论。俺来试着分析一下为何这是一种绝对要不得的诛心之论。<br />
首先，俺认为对人的道德评价，就像对人进行司法审判――道理是一样的DD应该遵从一种无罪推定原则。就是说，你必须有足够的证据，来证明艾先生是这样一个人，但目前的证据来看，并不充足，甚至一点都不充足。艾先生的个人历史都在那儿放着，尽管有许多争论，但基本事实谁也抹杀不了，比如以他这样生活富裕稳定、有名有利的人，他去从事调查汶川遇难儿童这样的事情，他去从事那些揭露统治者无耻的事情，从而将自己置于主流势力的对立面，他图的什么？有人说，名利。名倒是有，但这种名在现实中给他带来的麻烦，任何有理智的都应该承认远远大于他可能得到的利益。事实上，如果大家承认现当局仍是比较野蛮的，它的暗箱措施是远远超出常人想像的，那么就得承认艾先生面临的危险，并不比这些维权艺术家小，甚至比后者要大得多，头部被打、在德国昏厥动手术就是一例。<br />
我们应该有基本的是非判断，尽管这个世界足够复杂，但不能泯灭基本的是非。俺认为艾先生尽管可能有很多缺点或争议之处，看人看大节，他这么做，假若说超出你的想像力或者理解范围，不能认定他是为了做人原则、理想、自由价值、良心这些中国式犬儒认知的盲区，那么起码也别轻率地宣布这样一个人是卑鄙政客。如果这样的人是卑鄙政客，大捞特捞政治资本、个人名利，那请谴责者也来做一做试试，好不好？如果是这样，俺倒觉得这样的卑鄙政客多一些，或者对中国更好一些。<br />
俺还想分析一下，从事实效果的认定方面，好像也不能推断出艾未未鼓动艺术家上街，就一定是要牺牲维权利益、葬送维权大业、仅仅博取个人功名的肮脏之举。跟他去的人就没有头脑？就全是和他一样的人？这不大可能。俺认为这事的效果，有两种可能。一种是喻女士想像的，带来政府对维权的强烈镇压，激化矛盾；另一种则是让政府看到维权反弹的决心之大，为了维稳，它反而对开发商动手。这难道不是博弈吗？事实上开发商强拆艺术村已经不是单纯的经济事件，而是准政治事件；而艺术家的维权，实事求是的讲，出发点也不仅仅是像普通民众那样凭借肉体抗争，而是竭力在寻找各种力量支持自己，所以采取所谓暖冬行动，邀请媒体甚至外国人来观看，都不是制造政治影响吗？平心而论，喻女士之类的艺术家，内心里有没有从外界、从政治方面寻求支持的愿望呢？怎么艾先生一上街，就成了捞取个人名利的作秀？为何不把事件理解成业已进行的谋求政治支持的行动的延续呢？为何仅仅看到惟一的一种效果、而看不到另一种可能效果呢？如果艾未未是为了这后一种效果呢？<br />
证据如此不足，不完备，就愤然做出了上述结论，而不恤伤害一个极可能、其行为史已经表明他大节上是自由主义同道人的同胞，这是不是对个体尊严的极端漠视和麻木呢？假若很不幸，艾未未的举动被证实是鲁莽的，那损害的是艺术家们的维权利益；但更不幸的是，艾未未的举动尚未被证实是鲁莽的，也根本没有证据证明这是一追名逐利、自私自利的卑鄙政客的时候，喻女士的文章却已经伤害的艾先生的人格。喻女士的画俺没看过，但这样的文章过于粗鲁。<br />
而且不宽容，不同情，不悯恤。俺是这样想的：退一步说，即便是艾未未的行动有追求名望的成分，这也是人之常情。谁不希望自己有名声？追求名声就那么可怕吗？追求反抗专制的名声，更是要比追求一般名声好一万倍。对艾先生来说，俺觉得名声之于他，可能意味着生死存亡。名声大一点，伸向高智晟先生的黑手，再伸向他时，就会顾忌一分。当大家怀着事不关己的态度，鄙夷一个往往处于险境中的自由主义同胞追求名气时，可曾设身处地，替这位公民想想？他不过是为了更好保护自己的生命和尊严，为何我们总是这样残忍呢？<br />
最后一个问题，为何我们在丝毫没有证据、起码是证据不足的情况下，动辄对一个人做诛心之论呢？俺觉得1984某贴子下一个朋友：一些人，相信的是无利不起早，管闲事的人都别有用心。这就是生动的犬儒主义。长期的专制教育和环境濡染，让许多人感染了犬儒主义而不自知，或者沾沾自喜，或者以粗鄙、调侃、流氓、野蛮为荣。犬儒主义的一大特点就是把丰富多彩的世界，统统归于实利冲动，天下熙熙，皆为利来；天下攘攘，皆为利往。而极权之下的病态社会实践，尤其是统治阶层的行为，也在印证着这一点。久而久之，极权和后极权之下的民众不再相信除了利益之外世界上还有另外的行为动机。天下乌鸦一般黑，洪洞县里无好人。那么，自己自私自利、损人利己、破坏规则，也就心安理得，理直气壮，甚至充满骄傲，甚至自认成熟深刻，事实上这种成熟是烂透，这种深刻是病态。五毛们认为世界冲突无非是大国霸权和石油战略，任何国家都只有一种利益就是经济利益，世界上国与国之间就是你算计我我算计你，正像人与人――准确地说是极权、后极权社会的人与人之间的关系一样。而反对五毛、自认不是五毛的人，却习惯于轻率地把别人的任何行为上升为别有用心。<br />
这是一种评价他人的模式，同时，也是一种自我潜意识行为模式。犬儒世界观，同时也是犬儒方法论。相信他人根本不会为了超越一己私利去行动的人，自己也往往把行为框范在不择手段追求私利上。这样的人性，这样的潜规则，这样的病态，通不向自由，长不出美好，开不出鲜花，它的来源和归宿，都是后极权的酱缸。</div>

							
							
							
							
