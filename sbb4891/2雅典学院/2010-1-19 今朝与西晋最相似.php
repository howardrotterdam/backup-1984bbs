																											<div id="postmessage_441020" class="t_msgfont">常在网上看到一种说法，说今天与后清相似，我以为并不确切。<br />
<br />
在中国历史上直到今天为止的各统一朝代中，其实管理得最好的就是清朝。这集中表现在皇权一直是彰显而又严格，同时士大夫科举制度的意识形态功效也得到充分稳定的发挥，在二者的共同作用下，使社会在专制的法统下，依然取得了相当高的成就。<br />
<br />
即使到了后清的慈禧太后时期，政府的治理还都算清明严厉，比如在著名的杨乃武小白菜冤案中，因为审理的不当，当时撤消了浙江从省长到县相关的官员共达一百多人，并不怜惜培养一个干部的不易，这种力度，恐怕现在也望尘莫及。所以就能够理解，为什么在鸦片战争后，皇帝的权威受到打击，爆发了规模浩大的太平天国起义，居然被地主民团给镇压了，而这股力量还对朝廷保持着强烈的效忠，这在历史上差不多是绝无仅有的。试问今天如果碰到类似的事情，还能做到这个程度吗？<br />
<br />
清朝吏治的严厉，一直是有着良好传统的。比如在不怎么著名的嘉庆王朝，曾经出现过这样的事情。当时安徽水患，国家下拨赈济款项，山阳知县王仲汉趁机中饱私囊。后来中央政府派遣的稽查委员李毓昌到该地检查账目，书生意气，拒绝受贿。王县长见隐瞒不过，居然将李委员毒杀了。后来事情败露，不但自己掉了脑袋，皇帝还把当时的两江总督给发配新疆。<br />
<br />
这件事情如果放在今天会得到怎样的处理，怎么会牵涉到省长呢，还处理得这样重？省长难道就没有责任？作为一个大行政单位的总负责人，又有什么具体的政务需要他直接处理并承担责任呢？如果我们也像清朝这样来处理今天的恶性事件，比如矿难，直接罢免在任的省长，恐怕不知道会把这样的事故减少到怎样低的程度。<br />
<br />
在中国的历史上，有一个统一朝代的皇权最不彰显，就是西晋。那是个种姓制当道的年代，政治资源主要被盘根错节的氏族把握，寒门几无染指的机会，是皇帝与大姓共治天下的局面。<br />
<br />
没有了皇权的强大制约，氏族一方面“自由随性”地生活，穷奢极欲，又明目张胆地大肆破坏法律。其中最著名的例子就是巨富石崇，他当过几年荆州刺史，在这期间，除了加紧搜刮民脂民膏之外，还干过肮脏的抢劫勾当。有些外国的使臣或商人经过荆州地面，石崇就派部下敲榨勒索，甚至像江洋大盗一样，公开杀人劫货。这样赤裸裸的行径，恐怕在任何一个皇权强势的朝代都是不可接受的，在有清一代，皇亲冲击派出所都遭到了流放监禁的严重处理，如石崇这样的，恐怕早都被灭族了。而石崇的豪富在洛阳出了名，并且疯狂地奢侈比阔，当时有一个大臣傅咸，上了一道奏章给晋武帝。他说，这种严重的奢侈浪费，比天灾还要严重。现在这样比阔气，比奢侈，不但不被责罚，反而被认为是荣耀的事。这样下去怎么了得。晋武帝看了奏章，居然根本不理睬。<br />
<br />
清朝的皇帝为什么管理严格，因为他们是真心希望江山好，即使是出于私心，不管别人怎样，皇帝是确实在为天下负责任的办事情。而西晋则是一个无人负责的社会，这样的自由可千万要不得。</div>

							
							
							
							
