&nbsp; &nbsp; 历史和事实证明:<br />
&nbsp; &nbsp; 当穆斯林在一个国家的人口达到2％，该国穆斯林聚居区发生小骚乱；<br />
&nbsp; &nbsp; 达倒5％，该国穆斯林聚居区发生大骚乱，全国发生穆斯林引起的小骚乱；<br />
&nbsp; &nbsp; 达到10％，该国发生全国性的穆斯林骚乱；<br />
&nbsp; &nbsp; 达到20％，该国必然会发生穆斯林引发的战争，要么该国其他人口被杀而改信穆斯林，要么被穆斯林杀掉。<br />
<br />
E．南亚的印度。印度多年来遭受了不可胜数的宗教冲突。现在我就不得不为当初英国人的聪明理性所折服。<br />
&nbsp; &nbsp; 如果不分而治之，整个南亚大陆就是一片血雨腥风，死伤的人数就不会只有目前的数目了。<br />
&nbsp; &nbsp; 另外说一句，印度要追上中国，我还没又看到这个迹象，他还没有经历人均3000美元GDP的动荡时期，<br />
&nbsp; &nbsp; 种姓制度和宗教问题还处在酝酿的阶段，所有的问题一旦爆发，比起今天的中国所遭受的动荡更加痛苦。<br />
<br />
F．巴基斯坦身为一个伊斯兰教国家，同样不可避免极端伊斯兰教的蹂躏。<br />
&nbsp; &nbsp; 西部的俾路支解放组织要求独立分裂，落后部落地区成为本拉登的根据地。<br />
&nbsp; &nbsp; 俾路支问题充分反映了伊斯兰世界的分裂问题，世俗化和保守派无时不在斗争。<br />
&nbsp; &nbsp; 要么建立政教合一的伊斯兰国家，要么政教分离。伊斯兰教的倾向是回归政教合一的社会制度。<br />
&nbsp; &nbsp; 中国目前与巴基斯坦的良好合作关系，很大的原因是有与国际关系的需要，需要共同对付印度。<br />
&nbsp; &nbsp; 所以在宗教问题双方互相支持理解。<br />
<br />
G．阿富汗的恐怖主义政府由于被美国迅速果断的打击，暂时偏居一隅。<br />
&nbsp; &nbsp; 拉登政府公然建立原教旨的伊斯兰社会，公然与西方为敌，肆无忌惮的进行灭佛运动！<br />
&nbsp; &nbsp; 对人类的文明遗产实施灭绝。这就是反人类的罪行。想想吧，一尊尊石头佛像矗立在山脚下，<br />
&nbsp; &nbsp; 仅是个遗迹，有必要摧毁他么？我想不明白，当911事件发生时，我们中的某些人不理智的狂欢真是无知啊。<br />
&nbsp; &nbsp; 就事论事，911事件，是人类生命价值遭到摧残。<br />
&nbsp; &nbsp; 不管美国政府做过多少坏事，而历史的经验表明，美国会是一个功大于过国家。<br />
&nbsp; &nbsp;&nbsp;&nbsp;而出兵阿富汗，是得到人们理解的。<br />
<br />
H．哈萨克斯坦共和国。该国近年来在文化上倒向伊斯兰教是值得警惕的。<br />
&nbsp; &nbsp; 清真寺拔地而起，成为中亚重要的伊斯兰教地区。但是经过70多年的俄罗斯化，<br />
&nbsp; &nbsp; 接受了有效地洗脑的无神论教育，政治上摒除了政教合一，拥抱西方民族制度，<br />
&nbsp; &nbsp; 彻底导向伊斯兰是不会难么容易实现的。同时也因为俄罗斯族的大量存在，<br />
&nbsp; &nbsp; 社会经济的主导权并不是完全被伊斯兰掌控，俄罗斯对哈萨克斯坦境内的俄罗斯民族的支持是最大的保障。<br />
&nbsp; &nbsp; 但是哈萨克政府也有引狼入室的嫌疑，为了得到中东石油富国的支持和投资而有意无意的鼓吹伊斯兰，<br />
&nbsp; &nbsp; 支持伊斯兰教的发展，到处建立清真寺。我敢说，终于一天会自食其果的。<br />
&nbsp; &nbsp; 要知道，伊斯兰教对哈萨克突厥民族来说，也是一种外来宗教。<br />
<br />
I．俄罗斯的南部的车臣及其他共和国。包括高加索地区。伊斯兰恐怖势力制造的事件耸人听闻。<br />
&nbsp; &nbsp; 别斯兰造成大量的平民死亡，包括孩子妇女。该地区的恐怖分子也是要公然分裂俄罗斯领土，与俄军队对抗。<br />
&nbsp; &nbsp; 这是具有世界影响的圣战。包括东南亚，中国西部，北非，中东大量的伊斯兰分子都在此地区接受过圣战的洗礼。<br />
<br />
J．阿尔巴尼亚作为欧洲的唯一伊斯兰国家。伊斯兰教只是作为外衣而已。文化上已经被欧洲同化的差不多了。<br />
&nbsp; &nbsp; 世俗化是这个国家伊斯兰教最重要的特征。这也是为什么被欧洲接纳的原因。也有政治上的原因。<br />
&nbsp; &nbsp; 当初为了搞垮南斯拉夫，北约不得已出此下策。<br />
<br />
K．土耳其。土耳其是突厥世界的代言人，同时也是伊斯兰教的文化中心，<br />
&nbsp; &nbsp; 各种极端思想在土耳其大行其道，得到土耳其政府的庇护。中国的分裂分子很多聚集在土耳其。<br />
&nbsp; &nbsp; 中东的沙特因为拥有财富石油，成为了伊斯兰教的经济政治中心，由于拥有麦加成为宗教中心。<br />
&nbsp; &nbsp; 土耳其也经常发生恐怖事件，由于对世俗政府的不满，坚决要回复伊斯兰教原教旨社会。<br />
&nbsp; &nbsp; 鄙人的德语老师曾经作为交换生到德国学习，她告诉我们：在德国几乎没有买到假货，唯一的一次<br />
&nbsp; &nbsp; 就是在一个土耳其商店里，而且那个土耳其老板甚至想撕毁证据<br />
&nbsp; &nbsp; 总有一天，土耳其也要面对来自本身的伊斯兰动乱问题。<br />
&nbsp; &nbsp; 我敢断言：土耳其进入欧盟是永不可能的，除非彻底脱去伊斯兰色彩。<br />
<br />
L．非洲是我不得不大书特书的地方。非洲的落后，战乱不断。与近年来的伊斯兰在非洲的扩张不无关系，<br />
&nbsp; &nbsp; 非洲将会是伊斯兰的扩张的最重要战场。非洲中部即将出现一条横贯中部非洲的伊斯兰战线。<br />
&nbsp; &nbsp; 但是由于欧洲在非洲的传统利益，扩张将会非常激烈。<br />
&nbsp; &nbsp; 传统信奉基督教和原始宗教的非洲人不得不面对除了民族冲突以外的第二大冲突---宗教冲突。<br />
&nbsp; &nbsp; 近年来非洲中南部的穆斯林人口大肆增长，许多黑人国家开始了伊斯兰化进程。<br />
&nbsp; &nbsp; 伊斯兰教从非洲东海岸一致南下，沿海的地区几乎已经全部被伊斯兰教占领。<br />
&nbsp; &nbsp; 像索马里地区，伊斯兰教成为主导宗教。在非洲西部黄金海岸直到刚果湾，穆斯林也占据着优势。<br />
&nbsp; &nbsp; 中部的苏丹达尔富尔地区成为世界的焦点。<br />
&nbsp; &nbsp; 游牧的阿拉伯伊斯兰人占据了时代是农民的富尔人民的土地，将其驱逐，将其屠杀。<br />
&nbsp; &nbsp; 苏丹政府在达尔富尔问题上是幕后的真凶，多次为阿拉伯的屠夫提供武器，在富尔农民遭受屠杀时置若罔闻。<br />
&nbsp; &nbsp; 事情的起因不像某些学者宣称什么气候变暖，而恰恰是宗教冲突。<br />
&nbsp; &nbsp; 达尔富尔地区的黑人在得不到政府的保护的情况下，愤而组织自己的武装力量而实行自卫。<br />
&nbsp; &nbsp; 达尔富尔的受害一方几乎是手无寸铁的农民，信奉的是基督教和原始宗教，可以说，这场冲突的本质上已经属于宗教问题。<br />
&nbsp; &nbsp; 非洲的伊斯兰化进程不可逆转。<br />
&nbsp; &nbsp; 原因有二：<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;一是当地的黑人经济文化落后，对拥有经济优势的外来者伊斯兰分子根本没有抵抗能力，<br />
&nbsp; &nbsp; 很多地方还处在原始社会向现代社会的转变。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;二是外部势力的参透。<br />
&nbsp; &nbsp; 如中东的沙特阿拉伯成为非洲中南部最大的投资者，拥有非洲最大的农田，<br />
&nbsp; &nbsp; 近年来派遣大量的伊斯兰传教士进入黑人区，强迫居民信奉伊斯兰教。对顺从者给与支持，对不顺从者给与打击。<br />
<br />
M．欧洲近年来的穆斯林人口迅速增长，已引起当地社会的强烈反弹。荷兰的漫画事件成为伊斯兰攻击的目标。<br />
&nbsp; &nbsp; 英国警察对伊斯兰教徒执法时被要求戴上面纱，<br />
&nbsp; &nbsp; 法国的学校招收的穆斯林老师不尊重当地的社会风俗而坚决要求戴着面纱上课，<br />
&nbsp; &nbsp; 最后与学校对簿公堂，并且打赢了官司。真是天下奇闻！<br />
&nbsp; &nbsp; 穆斯林人从来不会与你讨论所谓的入乡随俗！只要求你尊重他们的风俗，他们没有必要尊重你的风俗。<br />
&nbsp; &nbsp; 这是强盗逻辑。<br />
&nbsp; &nbsp; 欧洲人所引以为豪的宗教自由政策现在成了伊斯兰邪教扩张的保护伞，<br />
&nbsp; &nbsp; 在欧洲现在谈论伊斯兰教成为人们的禁忌话题。悲哀啊！<br />
<br />
<br />
&nbsp; &nbsp; 粗略一看，在伊斯兰世界周围都存在不同程度的动荡，有人将其称为：染血的弧线。只有一种情况下才能够拥有和平----该地区或国家实现全部的伊斯兰或者将伊斯兰教全部剿灭！我不仅要问：难道都是别人压迫穆斯林吗？如果每个人都反对你，有可能是真理，也有可能是歪理。这时候就应该从自身上找问题了。<br />
<br />
&nbsp; &nbsp; 世界上没有哪个宗教像伊斯兰那样保守，顽固不化，自以为是，歇斯底里，流毒巨深。他们压迫妇女最为得力，连脸都要蒙起来；他们对异教的人实行排斥，要么同化，要么灭绝，<br />
印度人管他们这种极端的思想叫做converter，一心把异族同化，所以外族要娶伊斯兰女子几乎是不可能的；所以我们只看见有人皈依伊斯兰教，而没有人放弃退出伊斯兰教，而且他们还懂得要从娃娃抓起这样的道理，采用不断的灌输使人精神彻底失去独立思考的能力。从小就被教导----不容许异教徒的存在 .<br />
<br />
&nbsp; &nbsp; 看一看现在的中国和世界，我感到担忧：阿拉伯人在温州滞留的人数有几万人，西部的穆斯林不受计划生育政策的约束，享受超国民的待遇，拥有比汉族人口高几倍的人口出生率。<br />
最近新疆的7.5事件也证明了这些事情。世界上伊斯兰教超过天主教成为教徒人口最多的单一教派。并且基督教徒和伊斯兰教徒在信仰的强烈程度上存在巨大的差别。伊斯兰教徒对宗教都有一种近乎狂热的迷信。穆斯林人口目前是世界上人口最多的民族。他们对民族没有概念，只有宗教。只要是信奉伊斯兰教，就是为同一民族。<br />
<br />
&nbsp; &nbsp; 在广州，那些新疆伊斯兰邪教徒盗窃、抢劫、杀人等有违天理的行为猖獗的不得了；有的房东把房子租给新疆伊斯兰邪教徒开伊斯兰餐馆，结果那些被烧烤弄得乌烟瘴气，让它们走人，它们不仅不交房租，而且扬言要搞出人命！但是它们却由于民族政策的保护而屡屡逃脱法律的制裁！悲哀啊！<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp; 伊斯兰教扩张传播的历史就像是癌症扩张，像艾滋病一样涂炭生灵！直到今天，伊斯兰的丑恶面目才能被人窥知一二，真正到了全世界善良人民团结起来共同铲除这种反人类的极端的邪教的时候了！<br />
<br />
&nbsp; &nbsp; 本人在百度空间也遭受了谩骂和恶毒的攻击。请反对我的人，文明地列出你的理由：为什么绝大部分的恐怖袭击都跟伊斯兰教有关？<br />
