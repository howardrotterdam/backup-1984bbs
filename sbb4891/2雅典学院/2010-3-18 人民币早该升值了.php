																											<div id="postmessage_521527" class="t_msgfont">对金融不太懂，但看过一篇文章觉得有道理：<br />
说当前汇率不理顺，不仅易引起贸易摩擦，也影响国家的发展，如将上海建设成国际金融中心，就受制于此。因为按照现在的汇率和国内物价，外国人会觉得中国的物价非常便宜，一旦全部放开，外国人就会蜂拥而来抢购中国的资产（因为很便宜）。解决的方法有二：<br />
