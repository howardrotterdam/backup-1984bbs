二就是通货膨胀，把国内的物价提高。好处多多，不仅可以解决汇率的问题，而且当前国内储蓄额高达27万亿，犹如市场头上的悬剑，通货膨胀则可以将这柄悬剑摘除！两者方式其实对百姓都是利弊参半的。<br />
当前看来，国家主要将采用第二种方式解决汇率问题（估计汇率还是会小幅度上升的，配合大幅度的通胀），这样一方面可以煽动民族主义情绪，一方面可以悄悄的解决问题。即使出现问题也可以推到美国身上，但这样吃亏的还是百姓<br />
