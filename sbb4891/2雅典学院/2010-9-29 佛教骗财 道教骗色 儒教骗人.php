佛教追求清静无为，但落实起来却遇到一个麻烦：大家都衣来伸手、饭来张口地“无为”，谁来养活佛门弟子？靠吃斋是解决不了这个问题的。于是佛教被迫将手伸向了芸芸众生的口袋。唐僧师徒不远万里去取经，最后也被索贡，对此如来佛不但毫无羞愧之心，反而大言不惭地为之辩护。这也成就了佛教的特点：认钱不认人。可谓“佛门八门开，有理无钱莫进来”，哪怕你是个杀人犯，只要交钱，也可立地成佛。<br />
<br />
