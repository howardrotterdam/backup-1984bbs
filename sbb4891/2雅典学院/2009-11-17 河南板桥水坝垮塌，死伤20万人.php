																											<div id="postmessage_330381" class="t_msgfont">自然灾害之频繁已到了极不“自然”的地步。“板桥水库事件”洪水一片，群众大转移。（网络图片） <br />
<br />
汶川地震至今已两月余，被当局“天灾无情人有情”口号蒙蔽的人们欣赏着当局“全力救灾”的表演，视线被接踵而来的大暴雨、瓮安烧毁警局、上海袭警案等其它事故而转移，对地震背后真相的探索与质疑渐渐平息。至此，汶川地震彻底被当局定义为不可违逆的“天灾”。――天灾还是人祸？！当局历史上，还有哪些不为人知的“天灾”？让我们一起走近那些被当局尘封的历史。 <br />
<br />
一. 国外媒体揭开尘封的历史<br />
<br />
2005年5月28日，美国《Discovery》栏目编排了一期名为《世界历史上人为技术错误造成的灾害TOP 10》的专题节目。随着镜头的缓慢展开，一场场人为导致的灾难惨痛地展现在观众面前。它们包括前苏联切尔诺贝利核电站爆炸事件、印度博怕尔化工厂泄毒事件等。当报导逆向排名至TOP.1，观众们无法置信地发现――世界历史上最惨绝人寰的人为灾难，居然发生在中国！<br />
<br />
据《Discovery》节目报导：1975年8月，河南板桥水库因暴雨发生垮坝，9县1镇东西150公里、南北75公里范围内一片汪洋。现场打捞起尸体10万多具，后期因缺粮、感染、瘟疫又致14万人死亡。24万余的死亡人数直逼次年发生的唐山大地震！ <br />
<br />
二. 揭开血泪未干的真相<br />
<br />
板桥水库大坝位于河南驻马店地区、三门峡水库大坝之西南，是大跃进时代粗制滥造的产物，因工程质量粗劣、日常疏于维护，至灾害发生时，17个泄洪闸只有五座能正常开启。 <br />
<br />
1975年8月初，一场台风引发了当地历史上罕见的特大暴雨。河南泌阳县境内汝河上游的板桥水库水位暴涨，水库管理人员在没有得到上级命令的情况下，不敢大量排水泄洪，而外地区石漫滩水库的大量洪水急骤流入板桥水库，加快了板桥水库水位暴涨的速度。 <br />
<br />
8月7日19时30分，驻守在板桥水库的34450部队向上级部门发出特特急电称：“板桥水库水位急遽上升，情况十分危急，水面离坝顶只有1.3米，再下300毫米雨量水库就有垮坝危险！”在该急电未被回应的情况下，7个小时后的8日零时20分，水库管理局第二次向上级部门发出特特急电，请求动用飞机炸掉副溢洪道，确保大坝安全。 <br />
<br />
这两封急电均如泥牛入海，没有半点回应。40分钟后，高涨的洪水漫坝而过。水库管理局第三次向上级部门发出特特告急电，并紧急开启尚能移动的五扇闸门，但此时水库已经开始决口。8日凌晨，洪水像脱缰的野马，冲出板桥水库的决口，以每秒6米的速度铺天盖地向下游冲去。仅仅6个小时，板桥水库就向下游倾泄7.01亿立方米洪水。至遂平县境内时，水面宽10公里，水头高3―7米。昔日人欢马叫的遂平县城，顷刻之间一片汪洋。沉睡在梦乡中的人们，在浑然不觉中变成沉溺水底的冤魂。 <br />
<br />
洪水呼啸着向下游奔去，所到之处，水库垮坝，堤塘决口。决口的洪水与上游来水合二为一，汇合成更大更猛的洪水一路狂奔，铺天盖地的淹没了下游的城镇和乡村。据后来统计，整个驻马店地区96%的面积受灾，许多地方一片汪洋，平均水深3―7米，300多万人口被围困在洪水中。 <br />
<br />
直至此时，上级才决定炸开刘埠口小洪河左堤、洪洼（大洪河和分洪道之间的洼地）圈堤及河上阻水堤坝分洪，同时指示要确保该地区亚洲最大的人工平原湖宿鸭湖的安全――但为时已晚！几天之内，全地区大小26座水库相继崩堤垮坝，9县1镇东西150公里，南北75公里范围内一片汪洋。300多万群众被洪水围困，300多万间房屋倒塌，30多万头大牲畜漂没，洪水直接致10多万群众死亡，随后又有14万余灾民因此次灾害而丧生。 <br />
<br />
1975年8月7日，这是一个无数中原人民失亲丧友、泣血含泪，理应被记入史册以示警戒的日子，然而当局秉承一贯“报喜不报忧”的原则，用蘸满黑心话与灾民血的笔，将这一天轻松地从人们的视线与其D的历史中抹去了。 <br />
<br />
奇怪之处在于，在新闻脉络已经成型的1975年，很多60年代、70年代生人对于这场灾害完全不知晓。甚至事隔三十年后，很多中国人对于此次事故仍然一无所知。及至《Discovery》当期节目播出后，不少网友还抱着将信将疑的态度在论坛发帖求证，“板桥水库事件是真实的吗？是不是国外媒体的恶意杜撰？”其他网友的回复证实了该事件的真实性，“我便是此次事故的幸存者。那真是一场不堪回首的噩梦！” <br />
<br />
在一个宣称“民主自由，民众享有知情权”的国度，一场死亡人数达24万、部份村庄死绝的惊天惨剧，却被这个国家的执政D作为绝密档案，小心翼翼地藏进了历史的夹缝里! <br />
<br />
三. “天灾”还是“人祸”？<br />
<br />
“板桥水库事件”惨绝人寰，本应引起执政D足够的反省并引以为戒，然而，在当局统治下的中国大陆，如此灾难却不断重复地出现－－唐山地震、汶川地震…… <br />
<br />
有人这样为当局辩解――“气象灾害实属人力不可违，不应将罪责嫁于执政D头上”。这话初听有点儿道理，实则谬之千里！试问――无论是板桥水库垮塌，亦或是其后的唐山地震、今年的汶川地震，事先都有专家、研究人员向上发布警示预报，为何我们的执政D却屡屡反应迟钝？！倘若当局在临灾反应上，能有镇压“六四” 时期爱国大学生、镇压信奉“真、善、忍”的无辜的“FLG”民众时一小半的“魄力”与“快速反应能力”，数万平民怎会无辜沦为冤魂？ <br />
<br />
――到底是天灾还是人祸？！汶川地震灾民痛心疾呼：天灾不可违，人为更可恨！人祸胜于天灾！！是当局的知情不报、官场腐败、豆腐渣工程直接导致又一次惨绝人寰悲剧的发生！ <br />
<br />
追根究底，“天灾”沦为“人祸”的原因有二。首先是当局的体制腐败，从上至下办事互相推诿拖沓、效能效率极差，导致急件要件无法及时传递；其次，是因为执政D的核心机构对于“草根阶层”的漠视，而始终将其D、其政权之利益置于首位。譬如在汶川地震前，多名国内、国外地质专家已向政府递送临震报告，然而当局为营造奥运前所谓的“繁荣和谐”的“大好局面”不受动摇，居然残忍地将地震预报压下不报。于是，数十万本可通过事先预防而获得生还机会的百姓，就只能在无数毫无防备的情况下沦为冤鬼！讽刺的是，位于震区的核设施则因事先通知、采取预防措施而躲过了灾难。――当局不是宣称我国暂无准确预报地震的能力吗，何以厚此薄彼地的仅仅通知政府机关、机要部门，而置百姓于不顾？同是鲜活的生命，难道还可三六九等的区分谁轻谁重？！<br />
<br />
在这些事实面前，我们是否应该深思，在当局宣称的“天灾”中，“人祸”的成分是否更甚于天灾？！答案毋庸置疑是肯定的。 <br />
<br />
四.当局还对世人隐瞒了什么？ <br />
<br />
当今的中国民众，生活在当局人为营造出的舆论“一言堂”中，对敏感事件的真相知之甚少。人们的社会认知渠道来自于当局编造的教科书、内容经过“筛选”的报刊杂志等。隶属于当局统治的媒体直接成为国家统治机器，运用铁腕手段维护其D统治，扼杀一切不同之声。当局“禁书”那一连串长长的名单上，大多都是此类揭露历史真相、抨击当局强权统治的“异类声音”。 <br />
