2005年5月28日，美国《Discovery》栏目编排了一期名为《世界历史上人为技术错误造成的灾害TOP 10》的专题节目。随着镜头的缓慢展开，一场场人为导致的灾难惨痛地展现在观众面前。它们包括前苏联切尔诺贝利核电站爆炸事件、印度博怕尔化工厂泄毒事件等。当报导逆向排名至TOP.1，观众们无法置信地发现：世界历史上最惨绝人寰的人为灾难，居然发生在中国！<br />
<br />
据《Discovery》节目报导：1975年8月，河南板桥水库因暴雨发生垮坝，9县1镇东西150公里、南北75公里范围内一片汪洋。现场打捞起尸体10万多具，后期因缺粮、感染、瘟疫又致14万人死亡。24万余的死亡人数直逼次年发生的唐山大地震！<br />
<br />
板桥水库大坝位于河南驻马店地区、三门峡水库大坝之西南，是大跃进时代粗制滥造的产物，因工程质量粗劣、日常疏于维护，至灾害发生时，17个泄洪闸只有五座能正常开启。<br />
<br />
1975年8月初，一场台风引发了当地历史上罕见的特大暴雨。河南泌阳县境内汝河上游的板桥水库水位暴涨，水库管理人员在没有得到上级命令的情况下，不敢大量排水泄洪，而外地区石漫滩水库的大量洪水急骤流入板桥水库，加快了板桥水库水位暴涨的速度。<br />
<br />
8月7日19时30分，驻守在板桥水库的34450部队向上级部门发出特特急电称：&quot;板桥水库水位急遽上升，情况十分危急，水面离坝顶只有1.3米，再下300毫米雨量水库就有垮坝危险！&quot;在该急电未被回应的情况下，7个小时后的8日零时20分，水库管理局第二次向上级部门发出特特急电，请求动用飞机炸掉副溢洪道，确保大坝安全。<br />
<br />
这两封急电均如泥牛入海，没有半点回应。40分钟后，高涨的洪水漫坝而过。水库管理局第三次向上级部门发出特特告急电，并紧急开启尚能移动的五扇闸门，但此时水库已经开始决口。8日凌晨，洪水像脱缰的野马，冲出板桥水库的决口，以每秒6米的速度铺天盖地向下游冲去。仅仅6个小时，板桥水库就向下游倾泄7.01亿立方米洪水。至遂平县境内时，水面宽10公里，水头高3-7米。昔日人欢马叫的遂平县城，顷刻之间一片汪洋。沉睡在梦乡中的人们，在浑然不觉中变成沉溺水底的冤魂。<br />
<br />
洪水呼啸着向下游奔去，所到之处，水库垮坝，堤塘决口。决口的洪水与上游来水合二为一，汇合成更大更猛的洪水一路狂奔，铺天盖地的淹没了下游的城镇和乡村。据后来统计，整个驻马店地区96%的面积受灾，许多地方一片汪洋，平均水深3-7米，300多万人口被围困在洪水中。<br />
<br />
直至此时，上级才决定炸开刘埠口小洪河左堤、洪口（大洪河和分洪道之间的口地）圈堤及河上阻水堤坝分洪，同时指示要确保该地区亚洲最大的人工平原湖宿鸭湖的安全--但为时已晚！几天之内，河南省驻马店等地区、1万多平方公里的土地上，共计60多个水库相继发生垮坝溃决，近60亿立方米的洪水肆意横流，9县1镇东西150公里，南北75公里范围内一片汪洋。1015万人受灾，倒塌房屋524万间，冲走耕畜30万头，洪水直接致10多万群众死亡，随后又有14万余灾民因次生灾害而丧生。纵贯中国南北的京广线被冲毁102公里，中断行车16天，影响运输46天，直接经济损失近百亿元，成为世界最大最惨烈的水库垮坝惨剧。1975年8月8日，这是一个无数中原人民失亲丧友、泣血含泪，理应被记入史册以示警戒的日子，你们还记得吗？</div>

							
							
							
							
