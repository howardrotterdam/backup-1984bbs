																											<div id="postmessage_331855" class="t_msgfont">　　　　　　　　　　　　世界最大的水库垮坝惨案<br />
　　　　　　　　　　　　――1975年驻马店大水<br />
　　　　　　　　　　　　　　　　钱　钢<br />
<br />
<br />
　　被一再重演的水库垮坝悲剧，如同阴影，伴随着人类自进入“工业革命”时代以来的水库兴建史：<br />
<br />
　　1864年，英国戴尔戴克水库在蓄水中发生裂缝垮坝，死亡250人，800所房屋被毁。<br />
　　1889年，美国约翰斯敦水库洪水漫顶垮坝，死亡4000－10000人。<br />
　　1959年，西班牙佛台特拉水库发生沉陷垮坝，死亡144人。<br />
　　1959年，法国玛尔帕塞水库因地质问题发生垮坝，死亡421人。<br />
　　1960年，巴西奥罗斯水库在施工期被洪水冲垮，死亡1000人。<br />
　　1961年，苏联巴比亚水库洪水漫顶垮坝，死亡145人。<br />
　　1963年，意大利瓦伊昂拱坝水库失事，死亡2600人。<br />
　　1963年，中国河北刘家台土坝水库失事，死亡943人。<br />
　　1967年，印度柯依那水库诱发地震，坝体震裂，死亡180人。<br />
　　1979年，印度曼朱二号水库垮坝，死亡5000－10000人。<br />
　　……<br />
<br />
　　本文记述的事件发生于1975年，地点在中国中部河南省驻马店地区。在一次猝然降临的特大暴雨中，包括板桥水库、石漫滩水库在内的两座大型水库、两座中型水库、数十座小型水库、两个滞洪区在短短数小时间相继垮坝溃决。<br />
<br />
　　死亡人数，超过8.5万。<br />
<br />
　　由一场特大暴雨而引发整整一个水库群的大规模溃决――无论是垮坝水库的数目，还是蒙难者的人数，它都远在全球同类事件之上，这一天灾与人祸紧紧绞缠的惨烈历史，不能不令文明时代的人类铭心刻骨引为借鉴。<br />
<br />
<br />
　　　　　　　　　　　　　　　　　一<br />
<br />
<br />
　　1975年8月4日，该年度中国大陆第3号台风（“7503号台风”），穿越台湾岛后在福建晋江登陆。台风登陆的同时，恰遇澳大利亚附近南半球空气向北半球爆发，西太平洋热带幅合线发生北跃，致使这个登陆台风没有象通常那样在陆地上迅速消失，却以罕见的强力，越江西，穿湖南，在常德附近突然转向，北渡长江直入中原腹地。<br />
<br />
　　8月5日，行径诡秘的“7503”号台风在北京中央气象台的雷达监视屏上突然消失――由于北半球西风带大形势的调整，“7503”号台风在北上途中不能转向东行，于是“在河南境内停滞少动”，灾祸由此引发。<br />
<br />
　　“停滞少动”的具体区域是在伏牛山脉与桐柏山脉之间的大弧形地带，这里有大量三面环山的马蹄形山谷和两山夹峙的峡谷。南来气流在这里发生剧烈的垂直运动，并在其它天气尺度系统的参与下，造成历史罕见的特大暴雨。<br />
<br />
　　这个最易产生特大暴雨的地区，又恰是兴建水库的最佳区域――上百个山区水库，在这里如星罗棋布。<br />
<br />
　　1975年8月上旬在河南省南部淮河上游丘陵地区发生的这场特大暴雨，是水库设计者们未曾预计的。从8月4日至8月8日，暴雨中心最大过程雨量达1631毫米，3天（8月5日至7日）最大降雨量为1605毫米。最强大的雨带，位于伏牛山脉的迎风面，4日至8日，超过400毫米的降雨面积达19410平方公里。大于1000毫米的降水区集中在京广铁路以西板桥水库、石漫滩水库到方城一带。暴雨的降水强度极强，在暴雨中心――位于板桥水库的林庄，最大6小时雨量为830毫米，超过了当时世界最高记录（美国宾洲密士港）的782毫米；最大24小时雨量为1060毫米，也创造了我国同类指标的最高记录。<br />
<br />
　　目击者称：暴雨到来的数日内，白天如同黑夜；雨水象从消防水龙中射出；从屋内伸出脸盆，眨眼间水满；暴雨如箭矢，雨后山间遍地死雀。<br />
<br />
　　暴雨区形成特大洪水，量大，峰高，势猛。洪汝河在班台以上的产水量为57.3亿立方米，沙颖河在周口以上的产水量为49.4亿立方米。滚滚而至的洪水，对暴雨区内的水库群造成严重的威胁。<br />
<br />
　　板桥水库设计最大库容为4.92亿立方米，设计最大泄量为1720秒立方米。而它在这次洪水中承受的洪水总量为7.012亿立方米，洪峰流量17000秒立方米。8月5日晨，板桥水库水位开始上涨，到8日凌晨1时涨至最高水位117.94米、防浪墙顶过水深0.3米时，大坝在主河槽段溃决，6亿立方米库水骤然倾下，最大出库瞬间流量为7.9万秒立方米。溃坝洪水进入河道后，又以平均每秒6米的速度冲向下游，在大坝至京广铁路直线距离45公里之间形成一股水头高达5－9米，水流宽为12－15公里的洪流。<br />
<br />
　　石漫滩水库5日20时水位开始上涨，至8日凌晨0时30分涨至最高水位111.40米、防浪墙顶过水深0.4米时，大坝漫决。库内1.2亿立方米的水量以2.5－3万秒立方米的流量在5个半小时内全部泄完。下游田岗水库随之漫决。<br />
<br />
　　洪河下游泥河洼、老王坡两座滞洪区，最大蓄水量为8.3亿立方米，此时超蓄4.04亿立方米，蓄洪堤多处漫溢决口失去控制作用。<br />
<br />
　　驻马店地区的主要河流全部溃堤漫溢。全区东西300公里，南北150公里，60亿立方米洪水疯狂漫流，汪洋一片。因老王坡滞洪区干河河堤在8月8日漫决，约有10亿立方米洪水蹿入汾泉河流域。9日晚，洪水进入安徽阜阳地区境内，泉河多处溃堤，临泉县城被淹。<br />
<br />
　　据由中国原水利部长钱正英亲自作序的《中国历史大洪水》一书披露，在这次被称之为“75.8”大水的灾难中，河南省有29个县市、1700万亩农田被淹，其中1100万亩农田受到毁灭性的灾害，1100万人受灾，85600多人死难，致使纵贯中国大陆北京至广州的铁路（京广线）冲毁102公里，中断行车18天，影响运输48天，直接经济损失近百亿元。<br />
<br />
<br />
　　　　　　　　　　　　　　　　　二<br />
<br />
<br />
　　板桥水库和石漫滩水库兴建于五十年代“治淮”初期。<br />
<br />
　　淮河流域地处中国大陆南北气候过渡带，气候多变。在公元十二世纪以前，淮河水系完整，独流入海，水流通畅。十二世纪以后，黄河连续夺淮改道达六、七百年之久，带来的恶果是淤塞干、支流河道和入海出路，使中下游河道比降非常平缓，干、支流排水能力甚小，洪水流路十分不畅。<br />
<br />
　　1950年夏，淮河大水，灾害惨重。同年十月，中华人民共和国政务院作出了《关于治理淮河的决定》，确定了“蓄泄兼筹”的治淮方针，具体制定了“上游应筹建水库，普遍推行水土保持，以拦蓄洪水，发展水利为长远目标”和“低洼地区举办临时蓄洪工程，整理洪汝河河道”的战略部署。<br />
<br />
　　洪汝河是洪河与汝河的合称，为淮河上游两条较大的支流，古代是汝水的下游水系。汝水发源于今嵩县外方山，流经郾城、西平、遂平、汝南、平舆、新蔡，在淮滨县注入淮河。元代至元年间（公元1264－1295年），古汝水在郾城被截断，上游称北汝，入隐水即今沙河；下游称南汝，以舞水为源，仍循故道入淮。元代至正年间（公元1341－1368年），南汝上源舞水在舞阳西截入澧水。明嘉靖年间（公元1521－1564年），南汝水又在西平被淤断，舞水东出，入古意水，成为现在的洪河。原泌阳、遂平之清水遂为汝水之源，仍循古汝水旧道东南而下，即为现代的汝河。历史上古汝水流域是人类活动的密集地带，也是水灾多发地区，汝水三易其源无不与洪水泛滥有关。<br />
<br />
　　治水是人类古老的活动。在古代，这里有大量人工建造的“陂”――换成现代词汇就是小型水库。两汉时期，农田灌溉曾兴盛一时，新蔡的青陂、葛陂，汝南的鸿隙陂、石塘陂，泌阳的马仁陂等都是当时著名的蓄水灌溉工程。三国至隋唐，西平有二十四陂，遂平有张泽陂，正阳有慎陂，上蔡有黄陵陂。然而，宋元以来的一千多年间，水利工程日渐废圮。除战乱等原因外，水利工程自身也有一些严重的问题：重要的陂塘多串河截引，不能蓄泄自如，却常聚水成灾。<br />
<br />
　　五十年代的“治淮大战”中，在洪河上游修建了石漫滩水库，在汝河上游修建了板桥水库。据称，当时水文资料很少，设计标准很低。板桥水库在工程运用中发现输水洞洞身裂缝和土坝纵横向裂缝，于是，1955－1956年分别对板桥、石漫滩两水库进行了工程扩建。洪水标准按照苏联水工建筑物国家标准进行设计。板桥水库采用1％频率设计，0.1％频率校核（即通常所说的百年一遇和千年一遇）。校核频率3天降雨量530毫米，洪峰流量5083秒立方米，3天洪量3.3亿立方米。据此，决定大坝加高3米，坝顶高程为116.34米，防浪墙高程为117.64米；增辟辅助溢洪道，宽300米，底部高程为113.94米，连同原有的溢洪道、输水洞，最大泄洪能力为1742秒立方米，最大库容4.92亿立方米，其中调洪库容3.75亿立方米。石漫滩水库按2％频率设计，0.2％频率校核（即50年一遇设计500年一遇校核），校核频率3天降雨486毫米，洪峰流量1675秒立方米，3天洪量0.88亿立方米。据此，确定大坝加高3.5米，坝顶高程达到109.7米，防浪墙顶高程111.2米。经过加固，最大库容为9440万立方米，其中调洪库容为7040万立方米，最大泄量390秒立方米。<br />
<br />
　　这就是板桥、石漫滩两座水库垮坝前实际具备的工程标准。应当说经过改善、加固的两座水库，就其工程质量而言，并无严重问题。<br />
<br />
　　在板桥、石漫滩水库加固扩建后的三年间，中原地区的水库建设蜂拥而上，一发而不可止。仅1957－1959年，驻马店地区就修建水库100多座。如果说，“建国初期”板桥、石漫滩水库是让淮河大水逼出来的，那么此时，一大批新库的催生婆却是正轰鸣于中国大地的“社会主义高潮”。<br />
<br />
　　在大跃进的1958年，河南总结了漭河流域地区兴建山区水利的经验。当时的国务院副总理谭震林来河南视察时将其归纳为“以蓄为主，以小型为主，以社队自办为主”，并说：“现在山区问题解决了，要把漭河经验向平原推广。”<br />
<br />
　　一位名叫陈惺的水利专家当时即指出：在平原地区以蓄为主，重蓄轻排，将会对水域环境造成严重破坏――地表积水过多，会造成涝灾，地下积水过多，易成渍灾，地下水位被人为地维持过高，则利于盐分聚积，易成碱灾。涝、渍、碱三灾并生结果不堪设想。<br />
<br />
　　这位专家的忠告无人理会，“以蓄为主”的经验被大范围推广，很快便推及到安徽。在安徽境内，不仅丘陵地区涌现大批小水库，淮河流域的河道被也一道道“水坝”分割闸起，形成许多大大小小的“条状水库”，造成淮河流域在后来数十年间致命的“肠梗阻”症。<br />
<br />
　　“以蓄为主”一度成为水利建设的“纲”。<br />
<br />
　　陈惺是远东最大的平原水库――河南驻马店境内宿鸭湖水库的设计者。该水库施工时正值大跃进，省水利厅一位副厅长认为原设计过于保守，擅自作了几处关键的改动。例如：他认为“闸门设计太大”，便将原设计的12孔排水闸门砍去7门，仅剩5门。<br />
<br />
　　同此遭遇的是淮河豫皖交界处的班台分洪闸：该闸闸孔原按800秒立方米排水量设计，共为9孔，但由于“以蓄为主”的思想指导，水文数据被人为减小，只建造了7孔；1959年水闸建成后，在1961又人为堵闭2孔。<br />
<br />
　　中国在大跃进之后，进入“三年自然灾害”时期，河南亦如此。然而，现存的气象资料显示，河南省和当时整个中国，在1959－1961年之间并无特大自然灾害出现，但事实上中原大地却灾象频生：涝、渍、碱三灾闹豫，黄淮间饿殍盈途。无疑，各地水利建设所发生的严重失误增加了这次灾难的“人祸”比重。<br />
<br />
　　1961年，刘建勋调任省委书记，他认为河南灾大，“是水利方面的问题造成的。”为纠偏，他曾到处寻找水利工程的“反对派”。在信阳，他找到了发配在那里的“右倾机会主义分子”陈惺――大跃进中，陈惺对有人捐献不锈钢表带去炼钢表示不以为然，说：“用不锈钢表带炼钢主要是锻炼人，就是锻炼人代价也太高了！”这同他说“用放卫星的方式修建水库是劳民伤财”的话如出一辙。<br />
<br />
　　在1961年开始的“纠偏”中，河南省对全省的水库进行了一次全面的普查，对一部分设计标准偏低、施工质量较差、存有隐患的水库，包括一些大型水库予以废弃。<br />
<br />
　　但是指导思想的失误并没有真正被扭转。重蓄水灌溉，轻河道治理，重兴利。轻除弊的倾向依然顽强存在。到六十年代末，驻马店地区又新增加了100多座水库，与此相对照，洪汝河的排洪能力非但没有增强，反而一年年递减。<br />
<br />
　　1975年8月特大洪水到来之前的淮河上游地区，事实上已隐伏着严重危机：河道宣泄不畅，堤防不固，许多“病库”隐患未除。更为严重的是，对于板桥、石漫滩等大型水库可能垮坝的潜在危险，人们并无警觉――准确地说，“垮坝”二字在人们心目中更本就不存在。<br />
<br />
　　由于片面重视蓄水，忽视防洪，石漫滩水库在溢洪道上增加了1.9米的混凝土堰，板桥水库在大雨前比规定超蓄水3200万立方米。<br />
<br />
　　人们过于笃信“百年一遇”、“千年一遇”等既定的洪水标准，自信石漫滩水库可驭控50年一遇的洪水，在500年一遇的洪水中也能自保；板桥水库可以驭控100年一遇的洪水，在1000年一遇的洪水中也可无恙。<br />
<br />
　　谁也没有见过“千年一遇”的洪水，所有数据都是根据人们掌握的有限的史料推算而来――1975年8月，真正的大洪水来了。沛然而降的雨水使人们瞠目结舌：它的雨量，竟相当于人们所说“千年一遇”设计标准的两倍！<br />
<br />
<br />
　　　　　　　　　　　　　　　　　三<br />
<br />
<br />
　　“75.8”暴雨由三场降水构成。第一场暴雨出现在8月5日14时至24时。当日，板桥雨量站测得日降雨量为448.1毫米，最大1小时降雨量142.8毫米。而按水库“千年一遇”校核标准，最大日降雨量才是306毫米。板桥水库水位迅速上升到107.9米，已接近最高蓄水位。<br />
<br />
　　治水者最先尝到了被水治的滋味：因灌渠阻碍泄水不畅，大坝外的洪水淹没了水库的坝基。板桥水库管理局院内，5日晚积水达1米多深，电话总机室被水泡塌，电话线路中断，管理局与水库上游各雨量站全部失去联系。公路交通中断，板桥镇街积水二尺，供销社、信用社被大水冲倒，大部民房倒塌。板桥公社干部在慌乱中组织力量转移老人和儿童，派出所的干、警则紧急抢救被水浸泡的档案。<br />
<br />
　　8月6日早晨，刚从省里开会回来的驻马店地区革命委员会生产指挥部副指挥长陈彬到机关上班，发现指挥部成员已分头到基层防汛。此时，遂平县已对汝河随时可能溃堤提出警告，要求地区速送防汛物资。<br />
<br />
　　上午，板桥水库管理局报告灾情的人员赶到，他们汇报的主要内容是“板桥镇遭淹”，“群众生活发生困难”。<br />
<br />
　　陈彬提出：<br />
<br />
　　一、立即派出一个十余人的医疗队，抢救伤病员。<br />
<br />
　　二、群众家被淹，粮食也被淹，粮食部门要带粮食安排群众生活。<br />
<br />
　　三、商业部门负责解决煤等其它用品。<br />
<br />
　　四、通知邮电局抢修线路。<br />
<br />
　　地委副书记补充：以地委、地革委的名义慰问灾民和板桥水库的职工。<br />
<br />
　　当天，陈彬赶到板桥，地革会生产指挥长刘培诚也到板桥察看情况。当晚，刘培诚返回驻马店，陈彬为了“搜集一些好人好事”，留住板桥。<br />
<br />
　　这就是驻马店行政当局在8月6日对板桥水库灾情作出的全部反应。（陈彬在十余年后对笔者回忆：当时地区并没有感到板桥“情况紧急”，板桥水库素被称作是“铁壳坝”，人们对它的安全根本没有怀疑。）<br />
<br />
　　“75.8”暴雨的第二场降水出现在8月6日12时到7日04时。<br />
<br />
　　6日23时，板桥水库主溢洪道闸门已经提出水面，紧接着输水道全部打开泄洪。水位仍在上涨，库水位高达112.91米，而设计规定的最高蓄水位只有110.88米。<br />
<br />
　　7日中午，陈彬召集板桥驻军、板桥公社、水库有关负责人开会，紧急会商防汛事宜。会议中发现，水库及板桥镇完全没有防汛器材，没有木料、草袋、铅丝，也没有准备应付意外情况的炸药，――驻军名义上是炮兵，但有炮无弹。这时，通讯线路中断。<br />
<br />
　　午后，天奇黑，雨势更猛。从16时起，“75.8”暴雨的第三场降水――也是最大的一场降水出现，这场暴雨将持续13个小时！<br />
<br />
　　陈彬不懂水利，只感到“雨下得这么暴，库水泄得这么慢，情况有些不妙。”他再次召集会议，除请驻军使用连队报话机试图对外作接力通讯外，还紧急呼吁各级部门调集一切可以替代的物资参加防汛。<br />
<br />
　　板桥水库位于泌阳县境内，该县县委书记朱永朝于7日傍晚赶到板桥镇。面对板桥水库的危急形势，他果断决定：立刻安排水库下游的板桥、沙河店的群众迅速撤离，并协助陈彬拟就一份告急电报，通过军队辗转上送。<br />
<br />
　　这是8月7日晚19时许。<br />
<br />
　　与此同时，驻马店地区革委会生产指挥部正召开紧急抗洪会议，会上讨论了宿鸭湖、宋家场、薄山等水库可能出现的险情，唯独没有谈到板桥。据当时参加会议的人回忆，板桥水库根本就没有报险。事实是，一方面因为板桥与驻马店的通讯完全中断，一位携带报话机进行接力通讯的驻军士兵在行至沙河店时被汹涌的洪水卷走，再则，板桥水库因其坚固而无法使人联想到“垮坝”。<br />
<br />
　　与此同时，河南省水利厅在郑州召开紧急抗洪会议，会议的焦点是如何死守薄山水库，如何保住宿鸭湖水库，及石漫滩水库是否要炸副泄洪道的问题；也有人担心板桥水库的情况，陈惺在会上建议：速炸板桥水库副泄洪道，以增大泄洪量！但这一建议已无法传到板桥。<br />
<br />
　　截止到7日21时前，确山、泌阳已有7座小型水库垮坝，22时，中型水库竹沟水库垮坝。<br />
<br />
　　此时，板桥水库大坝上一片混乱，暴雨柱儿砸得人睁不开眼，相隔几步说话就无法听清。大批水库职工、家属这时正被转移到附近的高地，飘荡着的哭声、喊声和惊恐的各种声响在暴雨中形成一种惨烈的氛围。人们眼睁睁地看着洪水一寸寸地上涨，淹至自己的脚面，脚踝，小腿，膝盖……<br />
<br />
　　上涨的库水迅速平坝，爬上防浪墙，将防浪墙上的沙壳一块块掏空……水库职工还在作着无畏的抵抗，有人甚至搬来办公室里笨重的书柜，试图挡住防浪墙上日渐扩大的缺口……一位忠实的职工在暴雨中用斧子凿树，欲求留下洪水水位的痕印……<br />
<br />
　　突然，一道闪电。紧接着是一串炸耳的惊雷。万籁俱寂。<br />
<br />
　　暴雨骤然停止――夜幕中竟然出现闪闪烁烁的星斗。<br />
<br />
　　这时，就听一声惊叫：“水落了！”<br />
<br />
　　刚才还在一寸寸上涨的洪水，在涨至小腿、膝盖、腹部，甚至向人们的胸部漫淹去时，突然间就“哗”地回落下去，速度之迅疾使所有人都瞠目结舌――洪水的确在眨眼间退去。<br />
<br />
　　就在那些“老婆孩子”欢呼“水落了！水落了！”的时候，板桥水库管理局公安科长马天佑的脸色骤然间变白，刚才的那个霹雷，曾使他周身触电似的麻了一下；此刻，那座刚才还如同一只充足气的巨大气球似的板桥水库突然间萎瘪――6亿立方米的库水令人恐怖地滚滚下泄。<br />
<br />
　　“出蛟了！”――有苍老的声音在喊。<br />
<br />
<br />
　　　　　　　　　　　　　　　　　四<br />
<br />
<br />
　　从8月7日4时到8月8日1时40分，在20多小时之内，洪汝河上游的数十座水库相继垮坝。洪水向四面八方奔突，扫荡，又在四面八方合流扩展，在直立如壁的惊涛骇浪前，田园、村落、集镇……须臾间化为乌有。<br />
<br />
　　水库垮坝所致的大水与通常的洪水具有极为不同的特性，这种人为蓄积的势能在瞬间的突然释放，具有无法抗御的巨大的毁灭力量。<br />
<br />
　　板桥水库垮坝后，距水库最近的沙河店镇首先被大水吞噬。尽管泌阳县委书记朱永朝在事前作了紧急撤离布置，但因洪水势头凶猛，全镇6000余人中仍有827人遇难。<br />
<br />
　　撤离的通知，仅仅限于泌阳县范围。由于驻马店行政当局没有也不可能向全区作出相应的紧急部署，与沙河县仅一河之隔的遂平县文城公社，群众完全没有得到洪水警报，因此文城成为“75.8”洪水中损失最巨的地区：全公社36000人口中，有18000余人遇难；该公社魏湾大队1700余人中有近千人丧生；该大队三小队256口人中仅存96口，有7家人绝户！<br />
<br />
　　笔者曾访问当年的“三队”――如今的一个小行政村。“75.8”大水的幸存者说：大水下来前，我们咋知道水库会有危险呢？天黑时，村里人看见河南岸沙河店那边影影绰绰有人在比比划划大喊大叫，可风声雨声太大，根本听不清喊的啥！<br />
<br />
　　村民魏长河，全家6口人中有4人丧生。他回忆：喂饱牛时（约下午四时），雨已下得很大，天黑时，全队人都往地势较高的大队部躲。三个妮儿，俺家里的抱一个，我抱俩，手里还拉着一个十二岁的小子，刚进院子，眼看着大水就从高高的墙头扑进来，象盖被子似的把满院子人都闷在里面。<br />
<br />
　　当时五十岁的吴桂兰说：我和我十一岁的妮子被水闷住后，倒塌的墙就砸在我娘俩身上，险些被砸死，幸亏一个大浪把俺们托起，掀了出去，妮子眨眼间就不见了，我只觉着昏天黑地，抓住一张秫秸箔就随水飘走了。<br />
<br />
　　全家6口人中死去2人的魏世兴说：水来前，我正在找绳子，准备拉老父母上树上房子。父亲刚把绳子系到屋檐上，大水就进了屋，就看见那么结实的绳子跟一根线似的断了，再看，老父母已经随水冲远。<br />
<br />
　　村民魏东山回忆：我把老奶奶放进拖拉机的门楼子里，大水下来时，水头将奶奶和拖拉机一起卷走了。<br />
<br />
　　当时已74岁的小脚老大娘赵二妮，全家8口4人丧生。她说：我是见过大灾的，见到大雨下得这个样，我就在屋里蒸馍，蒸了一屉子又一屉子，眼见着雨愈来愈大，我把衣服都翻出来，穿上了两条裤子，三个上衣，连袄都穿上了，就听儿子喊，快走快走，快上大队院！俺娘俩还没有走到大队部，就听见轰隆隆一片房倒的声音，还不及挪步，人就漂在水里了。<br />
<br />
　　魏长河回忆：我是抓着房上的一只藤箩往下冲，一路冲到遂平城下，也不知喝了多少水。人说，县城南门、车站大桥和铁路是三道鬼门关，哪一道都是进去就出不来，我竟然都冲了过去，浑身的衣服撕得稀烂，一路上就听见大人哭孩子叫，一排排水鬼明晃晃的向你扑过来，后来才知道，那是露出水面的电线杆上的白瓷瓶。<br />
<br />
　　魏世兴：白花花的大水一眼望不到边，我在水里不知翻了多少个滚，憋得不能行，一露头，见两个大“淤杂”（草垛、杂物形成的飘物堆）向我冲来，其中有一棵连根拔起的大桐树，上面攀着许多人，乱哭乱叫，有人喊：“抱好东西！抱好东西呀！”我记得水里到处都有电灯一样明晃晃的东西。<br />
<br />
　　始终被洪水潮头“载”着往下游去的村民魏东山，一路东去，速度迅疾，犹如乘车，他说：大水冲毁了坟地，冲出了坟墓里的棺材，我是抱着一块棺材板才活了下来。洪水的水头足有几丈高，我浮在水头上面看前方的景物，人就象立在悬崖上，我记得大水冲过一处树梢，树梢下面有一所小院落，我清楚地看见屋里还亮着灯，有一个小妮子嘴里喊着“奶奶！”正往屋里跑，这时，“轰”地一声就全没有了。<br />
<br />
　　赵二妮老太太被卷到上百里外的阳丰大桥底下才被一棵树卡住，她挣扎着从树棵里爬出，临时穿上的几件衣服早就被洪水冲没，身上拉了许多口子，没剩下一块好肉。<br />
<br />
　　从板桥水库倾泄而出的洪水，排山倒海般朝汝河两岸席卷而下，文城拖拉机站75匹马力的链轨拖拉机被冲到数百米外，许多合抱大树被连根抛起，巨大的石碾被举上浪峰。板桥水库底部高程为120米，文城魏湾的高程为100米，遂平县城的高程为65米，县城东部的高程为50米。洪水就这样借着高程的落差顺势而下，板桥水库8日凌晨1时垮坝后，仅一小时，洪水就冲进45公里外的遂平县城，遂平县40万人，此时有半数漂没水中，一些人被途中的电线、铁丝缠绕勒死，一些人被冲入涵洞窒息而死，更多的人在洪水翻越京广线铁路高坡时，坠入旋涡淹毙。洪水将京广铁路的钢轨拧成绞丝状，将石油公司50吨油罐卷进宿鸭湖中。<br />
<br />
　　板桥水库垮坝5小时后，库水即泄尽。汝河沿岸，14个公社、133个大队的土地遭受了刮地三尺的罕见的冲击灾害。洪水过处，田野上的熟土悉被刮尽，黑土荡然无存，遗留下一片令人毛骨悚然的鲜黄色。<br />
<br />
　　翻越了京广铁路的洪峰，从西平、遂平两县境内继续向下游冲击，驻马店地区4.5万平方公里的土地尽成泽国。<br />
<br />
　　大量的洪水涌入著名的“险库”宿鸭湖水库，该库堤内堤外皆是白花花的大水，库堤只能容一辆卡车通过，苍茫中犹如一条飘飘欲断的细带。而就在这条细带上，拥挤着将近5万的灾民。<br />
<br />
　　宿鸭湖水库技术人员赵搭拉回忆：当时只听“咚！咚！咚”，堤外一座座泡在水里的房屋垮了。上游冲下来的“淤杂儿”、人，挤在水库的进水处，象旋涡一样打着转转。“救人呀！救人呀！”哭喊成片。眼看着一个个人栽在水里就没有了。<br />
<br />
　　侥幸的是，大水下来的八天前，宿鸭湖水库刚刚完成了抢修工程――由于该库常年隐患未除，下游有阻水工程，又因周围群众屡屡哄抢护坡石料而遭损坏，赵搭拉在1975年初两次上书国务院副总理李先念“报警”，在中央的干预下，有关部门终于批准了宿鸭湖水库的抢修工程。可以想象，如果没有这次抢修，这座“险库”也必然会在这次大水中垮坝。<br />
<br />
　　这条“细带”和这5万灾民的生命就这样被幸运地保存下来。<br />
<br />
