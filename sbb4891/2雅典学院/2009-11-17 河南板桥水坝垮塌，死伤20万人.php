　　<br />
　　（六） <br />
　　<br />
　　　　如果说水库垮坝所引起的冲击性灾害给洪汝河流域的百姓迅雷不及掩耳的毁灭性一击，那么河道宣泄不畅、洪水居高不下所造成的浸泡性灾害，则更加残酷地延续和加重了这场灾难本身的损失――水利建设指导思想上长期来形成的失误，无疑是对这次洪水中驻马店地区的八万余死难灾民和数百万受害生灵的摧残和扼杀。 <br />
　　<br />
　　　　在板桥、石漫滩水库垮坝失事后的第5天――1975年8月12日，以当时的中共中央政治局委员、国务院副总理纪登奎为团长、全国人大副委员长乌兰夫为副团长的中央慰问团抵达驻马店灾区。中央慰问团的专机到达李新店军用机场后，纪登奎等即换乘两架米－8直升机作空中视察。 <br />
　　<br />
　　　　水利专家陈惺陪同纪登奎。直升机起飞后，先在空中视察京广铁路以东的灾区，看了平原上的汝河和小洪河。在所视察的汝南、平舆、新蔡、上蔡和西平县的范围内见到的几乎是一片汪洋，5座县城和条条块块分布的高地如同散布在海中的岛屿。直升机飞行的高度仅50米，能清楚地看到每座“岛”上都密集着灾民。一些“岛”人多面积小，大量灾民不得不站在水里和爬在树上。 <br />
　　<br />
　　　　直升机继尔转向铁路以西，视察被冲垮的板桥、石漫滩水库。两座水库的大坝都在中段被冲开，形成深深的壑口。库水早已排完，露出干涸的库底。坝下沿河两岸，无论是村庄房屋还是道路树木，都被洗劫一空，留下一片荒凉。 <br />
　　<br />
　　　　中央慰问团在视察后认为，为了解救还困在水中的百万灾民，必须尽快排除洪汝河平原的积水。 <br />
　　<br />
　　　　水利专家陈惺向慰问团成员说明，这次受灾严重的洪河和汝河，在新蔡县城以南汇合后，向下至班台又分为两支：西支称大洪河，向南入淮河；东支称分洪道，经蒙河分洪道再入淮河。现在，由于大洪河的上段是在高地上切岗开通的，河岸陡峭，因此上游水位升高而河道流量增加甚少；分洪道虽然地势低下，但在进口处修有班台闸节制流量，也不能有效地扩大下泄流量。大洪河和分洪道之间的洼地称洪洼，历史上是大水时的自然行洪道，可这些年洪洼已被围垦，丧失行洪能力。这些因素都使得班台以上洪水壅积难下。当今之计，唯有使用爆破手段，炸开阻水部位加速行洪。 <br />
　　<br />
　　　　8月13日晚9时，河南省委书记刘建勋紧急通知陈惺，陪同慰问团成员农林部部长沙枫速回北京向国务院汇报。刘建勋含着眼泪对陈惺说：“你去，代表我，代表河南，只有一个请求，炸开阻水工程，解救河南人民。” <br />
　　<br />
　　　　14日0时15分，沙枫等抵达北京。 <br />
　　<br />
　　　　国务院会议室里，国务院副总理李先念已召集中央各部委和中央军委负责人等候开会。 <br />
　　<br />
　　　　沙枫首先汇报中央慰问团和河南省委关于加快排水的意见。李先念说：“为了救人，你们说炸开哪里我们就同意炸开哪里。”陈惺在地图上指出了需要爆破的位置，李先念在征求身旁水利部长钱正英的意见后，表示同意，并要钱正英起草国务院和中央军委的联合命令。 <br />
　　<br />
　　　　在钱正英起草命令时，李先念又问陈惺还有什么问题？陈说，宿鸭湖水库尚未脱险。李先念要陈惺立即和河南通电话，采取紧急措施，确保水库安全。深受板桥、石漫滩水库垮坝事件刺激的李先念冲动地说：“宿鸭湖再也不能出事，再出问题，它就是催命鬼！” <br />
　　<br />
　　　　钱正英起草好联合命令，李先念看后即和当时主持军委工作的邓小平通话，在得到邓小平的同意后，李先念下令武汉军区和南京军区的舟桥部队紧急出动，在中央慰问团的指挥下执行爆破任务。因为时间紧迫，李先念要求部队先行出发，由他亲手签字的命令随后由空军空投到各部队所在位置。 <br />
　　<br />
　　　　14日清晨，沙枫等返回驻马店。纪登奎决定由沙枫任组长、陈惺等为成员组成爆破指挥小组。陈惺向纪登奎转达李先念关于确保水库安全的指示，纪登奎说：“如果水库再出问题，我们就下地狱了，决升不了天堂。” <br />
　　<br />
　　　　指挥小组随即乘直升机去新蔡，转乘机船赶赴爆破点。上午十时，在武汉军区副司令孔令德指挥下，对最大的阻水工程班台闸施行爆破。巨响声中，班台闸所有的闸门、胸墙、桥面和部分闸墩都腾空而起，分洪口门由此打开。 <br />
　　<br />
　　　　全部的爆破工作进行了整整两天。 <br />
　　<br />
　　　　扩大行洪后，为确保下游淮河大堤的安全，纪登奎指示陈惺等人乘船直达蚌埠，入安徽境内察看灾情。只见沿途各地都在鸣锣疏散百姓，由于班台闸被炸，大量河南境内的洪水向下游倾泄，致使淮河中、下游的形势紧张。淮河多年受重蓄轻排指导思想之害，行洪道上早已围堰丛生，如同处处设卡，本在1956年就已达到抗拒1954大洪水标准的河道，此时的泄洪能力却已下降30－40％，河道上星罗棋布的“卡”造成淮河“肠道”的严重“梗阻”，陈惺亲眼见到沿途的淮河河道平堤流水，险象环生。 <br />
　　<br />
　　　　多年来，重兴利，轻除弊，重水库建设，轻河道治理，从神话中大禹治水的“疏导之术”，又退回到了鲧的“堵截”之法。一辈辈进化了的人，也愈来愈“聪明”地从“水库”中无休止地索取，却忘记了“水火无情”这个最简单的真理。 <br />
　　<br />
　　　　板桥、石漫滩垮坝14年后，曾经担任中华人民共和国水利部副部长的李锐在三峡工程论证中意味深长地指出，决不能片面强调水库的功效而忽略河道的治理，他说：“对河流防洪而言，堤防的作用是绝对的，是古今中外一切大江大河最有效的措施，是一件长年累月埋头苦干的工作，但显不出组织者的雄才大略、雄伟气魄，更不能扬名天下、流芳百世。但我相信，只要地球存在，河流存在，堤防的作用就是永恒的……” <br />
　　<br />
　　 <br />
　　（七） <br />
　　<br />
　　　　1975年11月下旬至12月上旬，水电部在郑州召开全国防汛和水库安全会议，会议由钱正英主持。 <br />
　　<br />
　　　　兹将水利部长钱正英在会上发表的讲话摘要抄录如下： <br />
　　<br />
　　　　“75.8”暴雨，对治淮工作是一次严格的检验，对全国的水利工作也提出了警告。因此，我们必须认真地总结正反面的经验，不断提高水利工作的水平。 <br />
　　<br />
　　　　对于发生板桥、石漫滩水库的垮坝，责任在水电部，首先我应负主要责任。我们没有把工作做好。主要表现在：首先是由于过去没有发生过大型水库垮坝，产生麻痹思想，认为大型水库问题不大，对大型水库的安全问题缺乏深入研究。二是水库安全标准和洪水计算方法存在问题。对水库安全标准和洪水计算方法，主要套用苏联的规程，虽然作过一些改进，但没有突破框框，没有研究世界各国的经验，更没有及时地总结我们自己的经验，做出符合我国情况的规定。三是对水库管理工作抓得不紧，对如何管好用好水库，对管理工作中存在什么问题缺乏深入的调查研究；有关水库安全的紧急措施，在防汛中的指挥调度、通讯联络、备用电源、警报系统和必要的物资准备，也缺乏明确的规定。板桥、石漫滩水库，在防汛最紧张的时候，电讯中断，失去联系，指挥不灵，造成极大被动。四是防汛指挥不利，在板桥、石漫滩水库垮坝之前，没有及时分析、研究情况，提出问题，千方百计地采取措施，减轻灾情，我们是有很大责任的。 <br />
　　<br />
　　　　板桥、石漫滩水库工程质量比较好，建成后发挥很大效益。但应兴建时水文资料很少，洪水设计成果很不可靠。板桥水库在1972年发生大暴雨后，管理部门和设计单位曾进行洪水复核，但没有引起足够的警惕和相应的措施，所以防洪标准实际上很低。 <br />
　　<br />
　　　　由于重视蓄水，忽视防洪，石漫滩水库在溢洪道上增加了1.9米的混凝土堰，板桥水库在大雨前比规定超蓄水3200万立方米，运用中又为照顾下游错峰和保溢洪道而减泄400万立方米。这虽对垮坝不起决定作用，但减少了防洪库容，提前了漫坝时间。由于事前没有考虑特大洪水保坝的安全措施和必要的物资准备，在防汛最紧张的时候，电讯中断，失去联系，不能掌握上下游讯情，不能采取果断有效的措施，也没有及早向下游遂平县发出警报，组织群众安全转移。 <br />
　　<br />
　　　　要总结治淮25年的教训，对洪水做出充分估计。1954年的洪水超过估计，1974年在山东、安徽发生的洪水也超过估计，今年(1975)也超过估计。据说，明万历年间，淮河曾发生过大水，跟今年的水情很相似。因此要很好总结十多年的经验。从气象、历史等特点找出规律性的东西。 <br />
　　<br />
　　　　钱正英的这番讲话，给人留下的深刻印象是：象板桥、石漫滩这样的水库溃坝事件，再也不能重演了！ <br />
　　<br />
　　　　她没有提，陈惺在20年前就指出过的重蓄轻排这种指导思想，对水域环境必然造成的破坏――无论你使用了什么计算方法，如何完善已建水库的管理，讯情出现时指挥多么通畅; 她也没有提，为什么陈惺的意见不但无人理会，陈本人又遭政治整肃，直到大祸临头才起用；她当然更没有提，造成这一切，包括决策与用人失误的体系及制度的原因。 <br />
　　<br />
　　轻轻的一句“我应负主要责任”，把所有的本应一桩桩、一层层追究检讨并且付诸法律的罪过都淡出了。结果是，在接下去的十几年里，仍是气魄宏伟地拦与蓄。到了1993年，还有好汉出来拍胸脯: “出了问题我负责!”①; 还要如1972年为给伟大领袖祝寿匆匆上葛洲坝一样，拿工程当政治筹码②。 <br />
　　<br />
　　 <br />
　　（八） <br />
　　<br />
　　　　上古大禹治水，因势利导，既尊重了人的生存愿望，祈望丰收，安居乐业，又尊重了水的特性，人水和睦相处，因而称誉于今。 <br />
　　<br />
　　　　在大灾难过后的第二年――1976年的初夏，这片曾经被洪水肆虐，埋葬过八万五千生灵的土地，以格外的肥沃赢得了一个罕见的丰收年。……仔细看去，沉默着的大地上，庄稼凹凸错落，厚厚薄薄，色彩浓浓淡淡，最令人心颤的是银璨璨的田野中那一小块一小块格外厚实格外茂密的庄稼。 <br />
　　<br />
　　　　那儿的麦子都长疯了！――幸存者说。 <br />
　　<br />
　　<br />
　　　　【注】(1)　这人是能源部副部长陆佑楣，他所说的“成立中国长江三峡工程开发总公司，虽然要等今年三月全国人民代表大会后，由国务院统一安排，总公司才能挂牌。但我希望现筹备处全体同志认真工作，大胆地干，即使干错了，理所当然有我负责!”(《经济参考》1993/2/28) <br />
　　<br />
　　　　(2)　陆又说: “三峡工程大江截流要争取在1997年完成，比原计划提前一年，使大江截流年度与香港回归祖国的年度同步，让1997年成为双庆之年。”(香港《文汇报》1993/3/10)<br />
　　<br />
　　----------------------------------------------<br />
　　看到一牛比句子，拿来做签名：<br />
　　兄弟一贯认为我国人口至少有18亿。如果愤青也计入总人数，那么5亿生猪存栏数也应该算进去。但关键不是人猪数量之比，而是政权由谁掌握，是在人手里还是在猪爪里。……即使在我党这样一个有着强大愤青传统的党，他们也爬不上去。</div>

							
							
							
							
