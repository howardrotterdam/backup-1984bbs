<br />
最近的博士门事件很有娱乐性。但又是件很严肃的事情，不论结局如何，都会促进国人对道德和诚信的思考和诉求。对打击中国社会各界的造假行骗之风起到正面的影响。<br />
<br />
<br />
改革开放三十年来，神州大地上博士无数，年产博士已跃居世界第一。这没什么稀奇，“咱家老姑奶奶多，表叔就多”。博士虽多，其中最为佼佼者，不过三人而已。但是，佼佼者易污，尧尧者易折。当此博士门闹剧愈演愈烈之际，这三人的状况不容乐观，其中一人已经掉入茅坑，另外两人也身置边缘。从这三人目前的举措来看，证明了“当局者迷”是条不破之真理。我看在眼里，急在心上。特此诚心相劝数语，以期三位博士翻然猛醒，割爱自救。<br />
<br />
秋雨大师教导我们，若相劝，必先含泪。不含泪无以示立场，不含泪无以见真诚。 所以，我就先去灶间切了几颗洋葱，才回来写了下面这几段话。<br />
<br />
1。 含泪一劝唐博士<br />
<br />
这件事由你而起。你说你现在很烦。我看岂止很烦，愁悔交加亦不为过。不用内窥镜，我也能看出你现在肠子的颜色。“问骏能有几多愁，恰似一位太监上青楼”。楼是上去了。但是被一个姓方的发现你那个玩意儿是个塑料的，上面仅一束野鸡毛而已。很多人说你还是很有能力的，可是，有能力的人还需要弄个塑料玩意儿吗?<br />
<br />
你现在该怎么办呢? 无非两条路。<br />
<br />
一是绝不认输。你可能心存侥幸，认为这是最佳出路，反正已经掉入茅坑了，索性拒不认帐，一路死抗。于是变成了石头，不仅又臭又硬，而且很黄很暴力。这样的话，全国人民就真不待见你了。<br />
<br />
第二条路是承认造假行为，坦诚道歉。我劝你把眼光放远。走这条路。事实上，即便你栽了，还是为民族工商业作出了贡献，以前的贡献已经被你宣讲了无数遍了。你没说的贡献是这几天才发生的。比如你令卖橡皮的商人们发了一笔。你还断了西太大在中国的生财邪路。虽说你的书也不会再版了，但是收藏升值的潜力增大了。<br />
<br />
千万要想开一点儿。其实，你落到今天这般田地，也不能全都怪你。中国社会的方方面面，都应部分地为你买单。这一点已有无数议论，我就不赘述了。只想指出一个说大不大说小不小的情况。那就是“总裁”一词的英汉翻译。我觉得这个词儿在某种意义上害了你。“President&quot;在美国可以是奥巴马，也可以是一个小学校长，还可以是一个社区桥牌俱乐部的头头儿。当一家洋人的企业来到了中国开拓市场的时候，也许派来的负责人仅仅是个副处级干部。但也抗着 “President”的头衔儿。翻译过来就是“总裁”。这个词儿在很多中国人的潜意识里是极有份量的。<br />
<br />
曾几何时，全中国人民只知道有一个人堪称总裁，那就是蒋总裁。“总裁手喻”可不是闹着玩儿的。每当听到这四个字，多少人要轰的一声起立敬礼。现如今来了个很能“喻”(亦或“娱”)的唐总裁，前面还捎着一串儿“微软”“中国”“荣誉”“终生”“职业”之类的形容词，就难怪人民群众要晕菜了。捧你为皇帝，也就情有可原了。你也就真以为自己是九五之尊，在忽悠全国人民的同时把自己也忽悠了。我曾看了一眼你的讲演录像，只见你当时口吐莲花，上嘴唇连着天，下嘴唇接着地，脸在哪儿? 没瞧见。<br />
<br />
现在，其羊已亡，是补牢的时候了。认了错，道了歉，还是好同志。人生就是一场赌博。要学会认赌服输。赌徒作弊，就是老千，老千回头，就是刘谦。骗子和魔术师的根本区别在于，魔术师向世人坦诚宣告他的把戏是假的，然后再把全国人民都骗了，然后全国人民还会为你热烈鼓掌，然后你还是成功的。然后你还可以成为青年导师，因为反面教材也是教材，仍然可以大量复制，人手一册。青年们仍然可以从你那儿学到东西。而且，在将来的讲台上，你又多了个英雄断臂自救的故事，岂不美哉? 人有丑闻在所难免，关键在于他面对丑闻的态度。软下身段儿来，说几句软话儿，坏不了你的大事。与其硬挺着作茅坑里的石头，不如当一块儿豆腐。会被好心人捞将上来，放在油锅里炸一下，还会招来不少食客。<br />
<br />
何去何从，望骏慎思。<br />
<br />
2。 含泪二劝习博士<br />
<br />
习博士殿下，给您请夏安了。<br />
<br />
其实我知道您今夏是不安的。外国有个微软，中国有个联想。现在国人由微软的唐博士联想到你习博士了。您那个博士是个什么成色，您自己心里有数，凡在国子监寒窗苦读过的监生贡生们心里更有数。他们不服啊！现在麻烦来了，您的肠子应该和唐博士的一样颜色了。<br />
<br />
与唐骏的动机相比，您对博士的追求令人费解。唐骏当年是一介布衣，搞张联络图来，也许为了将来弄个团长旅长干干。可您当年已经是闽粤总督了，还要那份儿劳什子做甚? 说您是为了学问，打死谁都不会相信。读博士作学问，首先要有作学问的底子和资质，然后要耐得住数年的寂寞。您的工农兵学历和外放大员的身份明确地告诉世人，您既没有那个底子，更无法耐住寂寞。想捞个清华的博士，您是天不时地不利，只有靠人和。<br />
<br />
但遗憾的是，唐博士可以说他的加州理工博士是编辑们吃错了药后奉献的。您习博士却不能说您的文凭是捧臭脚的奴才们主动进贡的。您更无资格说弄这个博士是“阔小姐开窑子，纯为图个乐儿”。<br />
<br />
我想最可能的原因，也许是您自知未学先仕，缺乏底气，急欲在翰林院补个名头。也许您的清华博士真帮您在顶戴花羚上又添上了两羽，毕竟它非野鸡所生。可您始料未及的是，您后来竟然被钦定为储君。盛名之下，您就当上了如今官场“仕而优则学”大潮中的“带头大哥”。看过天龙八部的都知道，带头大哥不是好当的，带着一段不光彩的历史，永远被良心折磨。<br />
<br />
“非翰林不入内阁”，是确有其事的，此乃祖制。但那是很久以前的事了，比如习博士太爷爷辈儿的前任，钦差林则徐，就是从翰林院出来的。那时的朝廷再腐败，也不敢在科举上胡来。乡试作假要砍脑壳，殿试作假要腰斩。想直入翰林院，非一甲进士不可。二三甲进士尚需通过皇帝亲自主持的朝考才成。相比之下，当今的储君和大小朝廷命官们，你们在诚信科考这一点上，连大清的同行都不如啊，不汗颜吗！<br />
<br />
现在，摆在习博士面前的也是两条路。<br />
<br />
一条路是保持沉默，然后责令刑部去把媒体们和谐一下。再让邸报发表一篇“能力是检验学历的唯一标准”的檄文，就算完事儿了。这是您最可能走的路。凡头上插着野鸡毛的，也都盼望着您走这条路。但是，这条路的后果很严重，要么没有人再敢说自己是博士，要么人人都敢说自己是博士。从此读书无用，造假有理。习博士您对博士学位的再定义就可以名垂青史了。<br />
<br />
第二条路是主动摘掉您的博士帽。并成立独立的评审机构，对衙门里的官员，凡是在职期间取得博士学位者，逐一进行严格的匿名评审。不合格者取消资格。既然朝廷对官员们的财产都嚷嚷着要盘查了。相比之下，审查官员们的学历要比翻出他们藏在各处老鼠洞里的银子容易的多了。这条路是一条光明大道。我这厢含泪顿首，恳请殿下明察。请还给“博士”这一名称它应有的学术尊严。这不仅是对学问的尊重，也是对诚信的尊重。此举于国于民，于您的声望，有百利而无一害。<br />
<br />
您要知道，对您的继位，很多老百姓是有看法的。百姓们平日里总嫌自己的子女们长大了还依赖父母，买房买车托儿费都要爹娘掏腰包。于是就给子女这一代起了个名字叫“啃老族”。其实，百姓们心里都明白，中国当今啃老族的带头大哥还是您习博士。您若是没有个老习在后面让您啃着，能从府台道台藩台干得一路青云，直至皇储吗?<br />
<br />
背着两个带头大哥的名声登基。您不觉得心虚吗? 显然，当啃老族带头大哥已经是没有法子的事了。但您可以丢弃另一个带头大哥的劣名。您若能整肃“仕而优则学”的歪风，拔除混在官员们顶戴花羚里的野鸡毛。那您就会给社稷带来一丝清明，给老百姓带来一点儿希望。也会让老百姓用另一只眼睛好好看看您。<br />
<br />
当然，您这么折腾的唯一风险，是储君可能当不成了。那又怎样? 当官不为民作主，不如回家听歌舞。您好福气啊，园子都不用去，家里就有位将军级的歌后给您唱。还不知足吗?<br />
<br />
说到将军级的歌后，我就再含泪多一句嘴。您在整肃“仕而优则学”的同时，最好捎带着把尊夫人的中将级军衔也免了吧。舞而优则校，唱而优则将，每多唱几年“希望的田野上”肩膀上就多一颗星星。这比“仕而优则学”还要来得可笑。虽说封的是“文职”，不一样在糟蹋军饷吗? 而且是将军级的。都什么年代了，在媒体网路及各种娱乐团体和形式如此发达的今天，专职为士兵们加油打气的文工团早没必要了。除非文工团员们还有其他的“任务”。<br />
<br />
甭管什么任务，保家卫国金戈铁马是男人们的事儿。前一阵子，朝廷竟派了些能歌善舞的女将军们到台湾献演，美其名曰统一战线。现在黄海那边儿有些火药味，不妨再派将军们冲着母舰上的美国鬼子高歌一曲，看看能否退敌。过去的朝廷羸弱，打不过人家，就只好送一些王昭君去，祈求和平。现在不是盛世崛起了吗? 那就让女人远离兵戎吧。否则，就真应了昭君这句戏词儿了：“退敌和番都要妾，不知何处用将军？”<br />
<br />
习殿下，您登基在即，这第一步可要走好啊！<br />
<br />
3。 含泪三劝毛博士<br />
<br />
毛博士夏安了。<br />
<br />
我猜想你若今夏也有所不安，倒未必是博士门闹的，恐怕与你的体重有关。<br />
<br />
与上述两位博士相比，你的博士帽水分最少。好歹你也曾坐了几年的冷板凳。只是你的博士帽戴得有些歪。再配上你那不衫不履的军容，实在是有损我军的光辉形象。<br />
<br />
据说你的博士论文和现在的研究方向是毛泽东军事思想。可是从你每次政协会上暴出的五花八门的提案来看，你实在是有些不着四六，不务正业。我看到的你最靠谱的提案是建议用毛主席的游击战争十六字诀来指导现代化战争。我不懂军事也不了解你的研究水平如何，但我想朝廷既然给了你兵部的差事，高就军事科学院战略部副部长，一定是有其用意的。希望你不要四处乱吃，八方题字。枉负了你的博士帽。潜下心来好好地作研究吧，下一次美国鬼子或俄国毛子打进来了，是地道战管用，还是地雷战好使。就全靠你的研究成果了。<br />
<br />
我这里含泪劝你割爱，不是让你扔博士帽。而是希望你扔了那句“我爷爷”的口头禅。<br />
<br />
每当看到你张嘴闭嘴地“我爷爷如何如何”，我就心如刀绞。因为，你的拳拳孙子之心，虽然日月可昭，但是毛主席不认你这个孙子之意，也是天地可鉴的。<br />
<br />
按理说，平常人家的老爷，听到孙子生出来了，一定是大喜过望，必令家人马上抱上堂来，好生亲抚一番，再合影留念，尽享天伦之乐。可是你呢? 自打你生出来，一个城里住着，老人家竟一次都不愿见你。如今你竟连一张和老人家的合影都没有。这正常吗? 他老人家根本就无视你的存在呀。<br />
<br />
你妈说你的名字是老人家起的并以此证明老人家是认了你的。谁能证明呢? 可有张不是来自“东太大”的白纸黑字? 玉凤那里连皇上今儿进了些什么，明儿拉了些什么都记载的清清楚楚，给皇孙起名这么大的事儿，怎么就一点儿记录都没有呢?<br />
<br />
有人说你妈不敢带你进宫见驾是怕江青的迫害，这就更令人笑掉大牙了。毛主席何许人也，他想认个孙子，看看孙子，江青敢叫板吗? 连毛侄远新江青都使劲儿的巴结，更不要说毛孙了。设想一下，她要是把你笼络住了，毛驾崩后她再令起诸候来，不更省事儿吗?<br />
<br />
还有人说主席身体欠佳所以不便见孙子，这也是胡扯。你出生之后，正是老人家频繁接见亚非拉元首的时候。难道老人家宁愿把哈喇子留在马科斯夫人的玉手上，也不愿亲亲自己孙子那可爱的小胖脸蛋儿吗?<br />
<br />
孩子，你醒醒吧。你想要个爷爷，可没人拿你当孙子啊！<br />
<br />
话至此处，我的热泪已经在夺眶了。<br />
<br />
其实，在毛主席心中，能传承自己的真正后代，仅毛岸英一脉，其他的，统统不认。早在五十年代的庐山会议上，老人家就向全党宣布了：“始作俑者，其无后乎”。自打那时起，老人家就彻底做了孤家寡人。什么儿子孙子的，一概没有。<br />
<br />
毛博士，听我一句劝，俗话说上杆子的不是买卖。既然老人家不认咱，咱就别腆着脸非票上这门亲戚不可。咱们有点儿志气，不当这孙子也罢！<br />
<br />
你仍然可以认真研究主席的思想，脱了这层干系，你的研究才会不偏不倚，才会更有些水准，才不会落入子为父隐的窠臼。<br />
<br />
。。。<br />
<br />
行了，打了这许多字，我也累了。三位博士，好自为之吧。<br />
