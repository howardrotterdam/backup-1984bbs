													<h2>解放军缺乏战斗力的十大理由</h2>
						
						
																											<div id="postmessage_427520" class="t_msgfont">部队待遇低、基础生活设施差、军人社会地位不高、部队风气极端不正、吃喝风，送礼风，检查风、体制极不合理、军费开支糊涂帐、缺乏打仗意识、对人才的不尊重、干部科学文化素质不高。 <br />
<br />
一、部队待遇低，尤其是野战部队（以我所在部队为例）。一个现役的副营职军官，工资大约为一千三百到一千四百，远远低于同等级别的国家公务员，相当于一个大中城市中等企业的职工的月收入，好多军官的家属下了岗，或者待业在家，他们的这些工资刚刚能够满足一家人一个月的开销。<br />
<br />
二、基础生活设施差，满足不了基本的生活保障，在现在的大多数团队，基本上是近千人共用一个不足一百平米的澡堂，且一个星期只能洗一次；上级规定的每天一个鸡蛋，一杯牛奶得不到落实，每天7.6元的伙食有不同程度的克扣，最可笑的是所谓的机关干部，解放军中的一员，他们竟然没有伙食费！言外之意，他们享受的部分的供给制，这对野战部队的“机关干部”尤其不公平。至于住的问题，是我始料不及的，当我第一次走进这个地处城中心的部队的单身干部宿舍时，我有一种恍如隔世的感觉，我好像回到了上个世纪六七十年代，低矮，阴暗，潮湿，看着近在咫尺的高楼大厦，我心里好像被几千根针扎。<br />
<br />
三、军人社会地位不高。在我们国家，随着改革开放和现代化建设的顺利进行，人民生活水平日益提高，军人已经不再是人们当年的偶像，相反，穷当兵的永远不可能在这个道德标准已经发生严重偏离的社会的尊重，９８抗洪才过去几年，为什么只有在那种时刻你们才想起子弟兵呢？现在我们的国人缺乏对军人的基本尊重，在各种公共场所设立的那些军人优先的窗口，那些是真正为军人开设的呢？军人在人们的眼中就是一个天生的受害者，是他们违背做为一个人的道德规范的堂而皇之的理由。随军军属的工作安置问题，有那些地方政府是真心为部队服务的呢？<br />
<br />
四、部队风气极端不正。现在的部队跑官，买官，卖官，可以说，相对地方，有过之而无不及。其张狂的气势，让人想起了电视剧里的大清朝。相对于地方，部队缺乏强有力的监督机制，所谓的纪律检查委员会，都是一些个领导兼任，形同虚设。贪污腐败是部队每个走上中级以上领导的必修课，也只有通过这一渠道，他们才能走上更高一级的位置，其中道理不必细说，这趋势现在正一步一步演化影响到了连级主官。<br />
<br />
五、吃喝风，送礼风，检查风。我所在是一个团，据不完全统计，我们一年迎接的检查组，工作组多达上百个，每一次迎来送往，都得吃好，喝好，拿好，有点纰漏就得被穿小鞋。每年花在这上面的经费，直接的，间接的，多达百万！正常的训练如何开展，训练搞的好，工作成绩不一定好！这在部队已经是至理名言了。不要相信报纸上报导的那些个标兵，尖子，都是宣传出来的。没有一个是真的。<br />
<br />
六、体制极不合理。大家都知道，司令部在一个部队的工作发挥着至关重要的作用，但在现在的部队中，财务，军需，油料，营房，组织，干部，装备部门才是最牛皮的，他们掌握着某些关系干部利益的东西，或是可以谋取一些私利的权利，而且由于他们的存在，导致一些正常的工作得不到有效的开展，一些想干工作的人会由于他们感到阻力重重。在我的团里，司令部的参谋，平时工作是最辛苦的，生活条件却是最差的，工作条件也是最差的。举个例子，电脑配备，装备口子上的绝对没的说，一色的Ｐ4，后勤差点，财务股在这方面按道理讲没有相关经费，可他们的机器配置是服务器级的。而司令部的同志们守着自己的266，433，可怜！<br />
<br />
七、军费开支完全是一本糊涂帐。我不是信口雌黄，可以说，找地方上任意一个搞财会的人就能从任何一个单位里查出许多问题。我们的主官，高干，他们有几个存款和收入相符的？有几个清楚知道自己一个月工资的数额的？因为他们根本不动用一分钱。我们的装备能保证几年不出问题，我们的营房能保证使用几年不返修，我们有多少东西是真材实料的，朱总理当年讲长江大堤是豆腐渣工程，王八蛋工程，我想，如果他看到了我们的一些营房，我们的一些电脑设备，他会如何评价？<br />
<br />
八、在干部的思想中缺乏打仗的意识。对于这个问题，我们也很无奈，我们没法把全身心投入到部队的建设中去，后院经常起火不说，部队是个养小不养老的地方，有关系的，有路子的成天想着如何提升，没关系的就想着早一点转业到地方，趁年轻，还不至于被社会所淘汰，这是摆在每个基层干部面前最现实的问题，可我们的部队经常把一个好好的人闲到三十几岁，级别也没上去，本事也没学到，然后投放到转业的洪流中，难怪现在转业干部要上访，我们的国家，我们的部队对得起谁？<br />
<br />
九、对人才的不尊重，人才浪费现象。这在基层部队最为严重，拿到一个本科生，他们不知道如何培养其成才，如何发挥其优势，尤其那些地方大学生，他们很多到了部队后，自暴自弃，不能说大学生的意识太脆弱，只能说我们和那些领导存在着太深的代沟，再举个例子：一个地方大学生到了部队，每天都要和排里的战士一起去搞菜地，他很不理解，情绪比较消极，某领导看到了，对此大学生所在连的连长说：他连这点活都干不好，他能干什么？这是真的。不知你们作何有感想？<br />
<br />
十、干部科学文化素质不高，这已经成了制约我军形成战斗力的主要瓶颈，现在我们基层部队的主官第一学历在大专的没几个，尤其是中级指挥官，他们对电脑，对新的科技产品，自身就有一种恐惧心理，对待上网问题，那是洪水猛兽。对待指挥自动化问题，他们根本没有形成一个根本概念，根本谈不上去下大力气实现它。而我们的基层军官，经历的指挥学院那如同炼狱般的生活，我想在这些方面，他们一样存在着相当大的差距。</div>

							
							
							
							
