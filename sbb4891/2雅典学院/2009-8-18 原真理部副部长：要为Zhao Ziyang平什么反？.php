													<h2>原真理部副部长：要为Zhao Ziyang平什么反？</h2>
						
						
																											<div id="postmessage_207583" class="t_msgfont"><a href="http://www.globalview.cn/ReadNews.asp?NewsID=18747" target="_blank">http://www.globalview.cn/ReadNews.asp?NewsID=18747</a>&nbsp;&nbsp;(环球视野)<br />
<br />
要为Zhao Ziyang平什么反？<br />
<br />
朱穆之　<br />
<br />
&nbsp; &nbsp; （作者为原新华社社长、原中宣部副部长、<br />
原国务院新闻办主任、中共十、十一、十二届中央委员）<br />
<br />
&nbsp; &nbsp; 一些人千方百计要为Zhao Ziyang平反，说他是“我党执政以来最好的总书记”，“改革开放的最大功臣”，“而遭到最不公正的待遇”，“是三十年来的新的大冤大错”。<br />
&nbsp; &nbsp; 根据Zhao Ziyang生前谈话录音，由其秘书鲍彤之子整理，在国外发表的《改革历程》一书，详细地阐明了他全面“改革”中国的“政治方案”。那么他的改革主张究竟是什么呢？<br />
&nbsp; &nbsp; 他的改革是为了推进中国社会主义建设吗？<br />
&nbsp; &nbsp; 不，他认为中国实行社会主义错了，决心“痛定思痛，改弦更张”。（《改革历程》3页）如何改呢？他认为就是要实行西方的政治制度，他说，“几十年时间里与西方对立的所谓新兴民主制度―无产阶级专政制度，在大多数国家退出历史舞台。倒是西方的议会民主制显示了它的生命了。…现在还找不到比它更好的制度。”“总之，西方议会制是现在可以找到的最好政治体制，…所以在中国目前条件下，首先必须确定政治体制的最终目标，是要实行这种先进的政治制度”。（297页）<br />
&nbsp; &nbsp; 他反对邓小平坚决反对采取西方国家的政治制度。他说，“邓对西方国家的多党制、议会制，是特别反对和拒绝的，差不多每次讲到政治体制改革的时候，他都要说上一段绝不可采取西方那样的政治制度”。（274页）<br />
&nbsp; &nbsp; 他反对四项基本原则。他说四项基本原则是“加在思想政治领域的一个紧箍咒”。（273页）<br />
&nbsp; &nbsp; Zhao Ziyang自认为是最讲民主的，他在“June Fourth”风波中坚决站在中央的对立面，正是为了维护民主。他是维护社会主义民主吗？<br />
&nbsp; &nbsp; 不，他要维护的是资产阶级民主。<br />
&nbsp; &nbsp; 他说，“总之，西方议会制民是现在可能找到的最好的…民主形式”。（297页）在这方面“台湾、韩国这些国家和地区，他们由原来那种体制逐步转变为议会民主制，有很多好的经验值得我们借鉴”。（298页）<br />
&nbsp; &nbsp; 他认为，要实行民主，就要取消共产党的领导。他说邓小平讲的扩大党内和社会上的民主，“都是不可能实现的，不过是空话而已”，因为“邓的信条是共产党的执政地位不允许挑战”。而要扩大民主，“势必触动高度集权政治”，（276页）<br />
&nbsp; &nbsp; 为了推行西方的民主，他主张实行多党制，由于“在中国目前条件下马上实行多党制不行”，因此他想把共产党领导的多党合作和政治协商制度，“真正丰富它，完善它”，实际上变为多党制，使党不能像过去那样“把一切权力都控制在自己手上”。“使社会上那些政治参与感强的人，通过民主党派这种形式实现自己的愿望，不致在体制外形成一种与党对立的组织”。（278页）<br />
&nbsp; &nbsp; 在经济上，他是要加强社会主义经济建设吗？<br />
&nbsp; &nbsp; 不，他主张实行西方一切由市场决定的市场经济。<br />
&nbsp; &nbsp; 他说，他认为，作为一个社会主义国家实行西方国家的市场经济体制，可采取逐步过渡的办法。他说，“在计划经济模式还没有作大的根本改革的情况下，中国经济中市场经济成分一天比一天多，所占比重一天比一天大，整个中国经济体制的性质也在（就）一天一天由量变到质变”。（139页）他认为，“一个国家实现现代化，要实现现代的市场经济，现代文明，它就必须实行政治体制上的议会民主制”。（297页）“如果我们不朝着这个目标前进，那么中国市场经济畸形状态，不健康的市场经济，……就不可能解决”。（298页）<br />
&nbsp; &nbsp; 自改革开放以来，在反左同时一直在继续进行的反右一反资产阶级自由化斗争中，他毫不含糊地明确承认站在资产阶级自由化一边。<br />
&nbsp; &nbsp; 他说，“我代理总书记后首先就要面对反自由化问题。我当时主张加以严格限制；缩小它的范围；要降温…。他们开了一个名单，要批判这，批判那，我把它压下来了，通过一些会议讲了话，保护了那些人。…所以‘June Fourth’以后，他们批判我时说我一上台就为反对自由化规定了种种限制，这倒是的确的”。（262页）<br />
&nbsp; &nbsp; 这就不难理解，为什么在Hu Yaobang下台不过三年，资产阶级自由化思潮又一次泛滥，以至爆发“June Fourth”风波。而在这一风波中，为什么Zhao Ziyang坚决与党站在对立面，推波助澜，把全国推到危险的局面。<br />
&nbsp; &nbsp; 但是党和人民是经得住考验的，终于平息了这场风波。并赢得了这些年来的稳定，各方面取得持续快速的发展。<br />
&nbsp; &nbsp; Zhao Ziyang的失败是必然的、合理的、公正的，一点不冤枉。那么，要为他平什么反呢？<br />
&nbsp; &nbsp; 通过《改革历程》，Zhao Ziyang终于自己显露了真相。原来认为他的改革是为了加强中国特色社会主义建设、推进社会主义民主建设的一心要为他平反的人。还要求为他平反吗？还能和他站在一起吗？<br />
&nbsp; &nbsp; 也有一些人要为他平反，是他们原来就反对共产党的领导，反对社会主义。现在有的人已经公开主张实行民主社会主义，要退回到旧社会实行资产阶级民主改革。他们现在当然要更加吹捧Zhao Ziyang，要求为他平反。<br />
&nbsp; &nbsp; 等待这些人的将是什么呢？被扫进历史垃圾堆。<br />
&nbsp; &nbsp; （《环球视野》）</div>

							
							
							
							
