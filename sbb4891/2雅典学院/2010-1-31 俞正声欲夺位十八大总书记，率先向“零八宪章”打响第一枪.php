&nbsp; &nbsp; 段春芳是首批签名 支持“零八宪章”的公民，上海法院为此找个理由，判其一年零六个月的刑罚，这也是在俞正声的授意下进行的。俞正声此举就是为了讨好胡锦涛，表明自己反动立场是与胡中央保持一致地，并敢冒天下之大不为，以赢取胡锦涛的信任和欣赏。其目的是希望胡锦涛能破除中共17大上已内定的习近平和李克强接班的计划。同时也向党内强硬派表明自己是在无产阶级专政事业接班人上的第一个硬强的代表，那还舍我其谁呢？当年“六四”事件前夕，俞正声和王岐山都曾向王震和邓小平、陈云上书强调：“杀他二十万，管他二十年”于是“天安门“才有了血腥的那一幕。俞正声梦想着自己的命运，也能像江泽民那样有着神奇的轨迹。1989年正是由于江泽民迫于恩人李先念等人的压力，率先动手拔掉了“世界经济导报”这面旗帜，诱发了从上海到北京乃至中国的一场政治风波，才使江泽民在“六四”后， 虽已年过65，有机会破格来了个“三级跳”，当邓小平和陈云推荐“李瑞环和李鹏”的双难选择中，脱颖而出登上了总书记的宝座。谁知道俞正声率先打响了向“零八宪章”的第一枪后，总书记命运的风向标是否会峰回路转到 自己的名下呢？（有报道中共十三大之前，邓小平拟内定为俞正声为第四代的接班人，只是因康华事件和随后的俞强声叛逃美国中共情报局事件，俞正声受到党内外指责未果）<br />
