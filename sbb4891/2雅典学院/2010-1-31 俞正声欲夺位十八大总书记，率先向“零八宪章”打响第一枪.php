																											<div id="postmessage_460075" class="t_msgfont">作者：盖正石<br />
<br />
&nbsp; &nbsp; 俞正声何许人也？原中共首任天津市委书记黄敬的公子（毛泽东的夫人江青曾与黄敬有过一段浪漫的姻缘）。当今中共中央政治局委员，上海市委书记，可是近日，俞正声因诸多问题，其名声已远远超过北京市市委书记刘淇和天津市委书记张高丽，而位居所有封疆大吏之上，名列第二，其“大名”仅次于远在重庆的薄熙来。不过，薄熙来是因为“唱红打黑”而列选亚洲最有影响的领导人物，而俞正声是因为“背红护黑”而成为负面的典型。笔者就近期国际媒体对俞正声所曝出的问题进行一次总结，发现俞正声有几大罪状，其罪恶指数已超过成克杰，陈良宇、陈希同等。&nbsp;&nbsp;<br />
<br />
一． 包庇黑社会，坏过陈良宇<br />
<br />
&nbsp; &nbsp; a. 谁都知道陈良宇出事，是因为包庇上海首富周正毅的社保案，使周正毅重罪轻判，干扰司法而获刑入狱。俞正声主政上海，当吸取教训，并对因陈案而受牵连的著名大律师郑恩宠给予积极的评反，才是俞正声的为官之正道。可是，我们看见俞正声主政上海对郑恩宠不仅没有平反，相反在不久前胡锦涛视察期间，连要洗澡的郑恩宠的出入家门权，都给剥夺了。<br />
<br />
&nbsp; &nbsp; b. 上海著名证券律师严义明被打案，俞正声迫于压力先批示抓捕凶犯，待上海公安局把凶犯抓捕后，俞正声以政治任务的名义下令保护黑社会，致使两名主犯没有走上审判台，被取保候审，四名从犯被重罪轻判，对张宏伟调查的程序根本没有进行。2009年12月，刘杨等四名打手在编造了打人动机，而没有被徐汇区法院采信的情况下，刘杨等四名黑社会被以伤害罪分别判处两年六个月以下的有期徒刑。此判决一出，上海市民们愤怒不已，人们都认为俞正声比陈良宇胆子更大，心眼更坏。<br />
<br />
二． 有错不改，不让冯正虎回家<br />
<br />
&nbsp; &nbsp; 为上海市民积极维权的著名律师冯正虎，已住在日本成田机场，露宿八十八天了。身为上海市委书记，封疆大吏的俞正声，假借世博会安全的名义不让冯正虎回家，在胡锦涛的过问下，俞正声迫于压力，虚情假意地让外交部驻日使馆人员看望冯正虎，但得知冯正虎依法要追究其法律责任时，又原形毕露，中国使馆官员“正告”冯正虎：“要回国先进日本国境，不能让上海方面道歉，否则你就在成田机场躺着吧，世博会开完后，再议冯正虎回家之事。”公民回家，回国是最起码的人权，俞正声一介纨绔子弟出身，何时把人权放在眼中，在他眼里有的只是皇权，党权和太子们胡作非为的权力，其实，俞正声对黑恶势力的保护，早在湖北时就已经开始了。<br />
<br />
三． 对彭真大公子傅洋涉黑充当保护伞<br />
<br />
&nbsp; &nbsp; 2003年初，航行在长江的“东方皇后“号游轮倒卖大宗毒品交易，被公安部和湖北省公安厅发现，武汉市公安局调动警力，拟待”东方皇后“号靠岸时突击检查，可是当警员们待”东方皇后“号停稳时，公开身份上船检查时，遭到了“东方皇后”号的保安武装抵抗，多名警员被保安用霰弹枪击伤。当湖北省公安厅调集大量警力包围了“东方皇后”号时，正是俞正声在接到其“太子党“兄弟傅洋【彭真的儿子】的电话后，命令湖北公安厅停止行动，一些受伤的警员气愤不过，到北京向“法制日报”和“人民日报”反映，又是俞正声出面让时任中央政法委书记的罗干下令停止了“法制日报”和“人民日报”的报道。<br />
<br />
四． 为邓质方霸占农民土地大开杀戒<br />
<br />
&nbsp; &nbsp; 2002年末，武汉东西湖区所属农场的农民四万亩土地，被所谓的一个“台湾高新技术开发区”一夜之间征用。农民们四处告状，俞正声下令调动数千名警察对手无寸铁的上万名农民进行镇压，数百名农民被抓，近百人被打成重伤，轻伤者无数，武汉医院一时人满为患。在国际媒体介入报道后，俞正声把责任推给了从辽宁调到湖北当省长的张国光，（张国光在辽宁时，也因镇压百姓而名声狼藉，被中央调至湖北），张国光随即被中纪委双规。张国光至今在秦城监狱中喊冤无人听。事实上，是邓质方在周北方事件后，中央命令邓质方不得回国，这是卓琳向江泽民和曾庆红的承诺，可是，耐不住寂寞的邓质方在美国没法施展淫威，于是偷偷潜回中国大陆，跑到湖北同俞正声一起抢夺农民的土地，邓质方利用俞正声的支持，先后在东西湖建立了中国第一家跑马场进行中国富人的暗中豪赌。中国的富豪们凡是需要邓二公子保护的，都到俞正声处拜码头。俞正声在其弟弟俞强声叛逃美国后，正需要邓朴方重新支持与信任，为了向上爬，俞正声非抱紧邓大公子和邓二公子的大腿不可。东西湖区的四万多亩土地，让邓质方肥肥地捞上了上百个亿，等到中央追问下来，俞正声把张国光推上前线，当了替罪羊。<br />
<br />
五． 包庇刘志祥。为了自己的政绩，首创“打黑经济学”<br />
<br />
&nbsp; &nbsp; 铁道部长刘志军的弟弟刘志祥在湖北省有四大天王之称，“玩女人大王”，“打架大王”，“贪钱大王”，“花钱大王”，在武汉市无人敢碰，其原因就是自己追随其兄当过中央首长专列的司机，自吹为“三品大员正处级干部，胡锦涛的抬轿人”，由于刘志祥任汉口车站站长，武铁分局副局长，贪钱无数，玩女人无数，又是个不学无术的法盲，曾在几年中五次结婚，并亲手杀死了一位潜江的小姐，后又雇凶杀死了举报人高铁柱。俞正声看在眼里，只是不管，等到高铁柱家人在中央告了御状，俞正声利用刘志祥无恶不作的所有证据，对刘志军发起进攻，并迫使刘志军在刘志祥要判死刑的前夜，挂专列亲自到武汉的东湖宾馆面见俞正声。由于刘志军慷慨修改中国的第一条高速铁路计划，才救下刘志祥这一恶贯满盈的流氓狗命。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 前不久，重庆市委书记薄熙来在重庆“唱红打黑”把汪洋的老巢剿了，气得汪洋没有办法，只好拿出广东财政的五百亿到重庆去给薄熙来送大礼，并美其名曰“支持西部大开发”、“支援西南建设”薄熙来此招就是学的俞正声之术。本来这1168亿是薄熙来主政辽宁时，当过沈阳铁路局局长的刘志军，已把中国第一条铁路建设的线路由北京规划到沈阳，当数薄熙来的政绩。可是没有办法，只因给刘志军当官的玩命支持的刘志祥不争气，干事不利索把小命送到了俞正声手中掌握，薄熙来到手的肥肉被抢了个正着，俞正声只是让秘书打个电话给宜昌市中级人民法院院长，1168亿的政绩就书写完毕，真可谓是大手笔之动作。几年之后薄熙来明白了，要出大政绩得学俞正声的打黑“经济学”。“打黑经济学”的最大特点是，没有成本，一打万利。你看薄熙来一打黑，汪洋老老实实送上五百亿。虽然成果不如俞正声的千亿大，但也是成绩不斐啊。<br />
