　　<br />
<br />
 <br />
　　最近上网发现，笔者经常用的两个资料库被挡在了中国互联网围墙之外。其中一个是互联网电影数据库，这是收集美国电影资料最为齐全的资料库；另一个是英文维基百科全书，文字部分虽然能看到，但插图却无法显示，作为一个百科全书，没有插图显然让很多内容大打折扣。<br />
<br />
 <br />
　　以前youtube被屏蔽时，虽然也令人不满，但毕竟是娱乐网站，看不了也就算了。而电影数据库和英文维基却是三天两头都要“光顾”、查找资料的。网页打不开或者打开缓慢，让我这样的守法公民也不得不用代理服务器“翻墙”了。<br />
<br />
 <br />
　　全世界最受欢迎、服务最好的网站，大部分被挡在中国互联网之外，如facebook, twitter, youtube, wordpress等。现在google也威胁退出了。现在许多人担心的问题不是谷歌是否撤出中国，而是担心google所有的服务都可能被屏蔽掉，这对用惯google的人来说，是很让人恼火的。<br />
<br />
 <br />
　　互联网之所以具有划时代的意义，就是因为它打破了传统的地域和国界的划分，人们在世界各个角落都可以无障碍地交流、沟通、信息共享。这是互联网最突出的优势。有网友戏称中国网络是“大中华局域网”，好像外面世界一个网，我们一个网。有人说，我们也可以自己玩，没有youtube我们有优酷，没有twitter我们有微博，没有facebooke我们有开心网……长期地模仿下去，我们不但会失去自己的创新能力，我们更恐怕会错失整个互联网时代的众多发展机遇。<br />
<br />
 <br />
　　过于严格的审查制度，不利于信息流动，反而丧失了互联网最根本最突出的优势。电影网站上，虽然个别电影犯禁了，但大部分是没有问题的；维基百科的个别词条或许有失公正，但绝大部分词条对科普、科学研究和知识积累有着非常重要的作用。如果只是影响了娱乐倒也罢了，但如果因噎废食，使部分人工作、学习感到不方便，只会逼着大家“翻墙”，使用代理服务器，如果是这样，网络管制就起了反作用。<br />
<br />
 <br />
　　与此同时，中国互联网越脱离世界，就越难吸引海外人才回国，对中国软实力的损害就越大。用 facebook交友、twitter发布信息、youtube娱乐、wordpress写博客、wiki查资料、imdb查电影信息，是国外普通老百姓日常生活的一部分，许多在海外的华人也同样用习惯了。不能小看了这些互联网习惯对海外华人的影响，如果回国必须以割舍掉跟许多亲朋好友的联系为代价，恐怕很多人心里就会重新掂量掂量了。现在中国正处在转型的关键期，引进高端的专业人才，就像当年“两弹一星”回国的那批人一样，能大大推动中国未来的能源、科技、军工等领域的发展。<br />
<br />
 <br />
　　当下，全球化已经成为一个不可逆转的大趋势，而互联网技术的广泛使用正是基于这样的趋势潮流而动应运而生的。中国作为一个正在崛起的新兴大国，不应该也不可能逆潮流而动，过分收紧对互联网的管理，逐步开放是大势所趋，否则中国将成为互联网的一个孤岛，重回闭关锁国的状态。随着改革的深化和国力的壮大，中国的心态应该更加成熟和自信。尽管互联网上各种信息鱼龙混杂，开放网络必然会给中国带来一定程度的冲击，但也是中国塑造国际形象的机会，从长期来看是利大于弊的。把世界上最流行的网络拒之门外，不是一个开放的、宽容的大国应有的心态。<br />
<br />
 <br />
　　【方舟子按：这是编辑根据我的文章《中国正在成为互联网的孤岛》和对我的电话采访整理的，所以有些地方的文字风格不像我的，本来应该做一些改写，但是昨天待在电视台录了一整天节目，录完时已近晚上11点，只赶得上在发稿前确认了一下内容无误，没法做文字润色。】（本文发表于2010.1.18《环球时报》）<br />
