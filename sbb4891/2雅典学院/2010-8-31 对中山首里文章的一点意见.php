													<h2>对中山首里文章的一点意见</h2>
						
						
																											<div id="postmessage_806241" class="t_msgfont">因为言论和观点的原因，方舟子近日遭到歹徒的武力袭击，正如前年钱列宪遭到袭击一样。<br />
<br />
在我看来，歹徒这种以暴力压制言论的行为是一定要在道义上受到舆论的谴责，要在行为上受到法律的制裁。<br />
<br />
然而，令人齿冷的是，居然有人因此对方舟子进行谴责，对歹徒进行褒奖。尽管在这片神奇的土地上，神奇的事情屡见不鲜，我还是被中山首里《论方舟子遭袭：王爷身边的狗奴才被打 是活该还是不活该？》这篇奇文雷到了。<br />
<br />
我一向不愿意去揣摩别人写文章的动机，一向只就文章表达的内容和想法进行思考和辩驳。但这次，我真的想问问，中山首里您写这篇文章的动机是什么？上一次，我出现这种想法是看到一位网名叫“老田”的左派人士论证张志新被杀合法合理。<br />
<br />
中山首里说，“台下的你再流氓你流氓的过镰刀斧头帮？你再土匪土匪的过伟大光荣的一贯道？最歹毒的货色业已存在，何来还有把至今身份不明的袭击者，现行扣上“歹徒”这样的字眼，把他们归并入镰刀斧头帮的行列呢？”这就好比说，一人砍掉您的胳膊是伤害，另一人只砍掉您的手指头就不是伤害。通过偷换概念，您轻而易举地把袭击者从歹徒的行列中挽救出来。<br />
<br />
中山首里说，“为什么那两人是不能被方舟子舔一贯道的股沟惹火，清理其的屁眼而感到恶心，而想把其人从地球上抹去，向世人证明任何与一贯道眉来眼去，水乳交融之徒，虽无权利持镰刀斧头砍杀，但是借把锤子也要吓死鸡鸣狗盗的货色呢？为什么他们不能是看不惯政治正确的嘴脸，在一贯道的环抱里飞扬跋扈的无耻文人，无耻作家、无耻“院士”的表演，想借此表达下亲共者虽远必诛的豪情呢？”这就好比说，因为一人觉得您说话恶心，就可以把您抹去。这样赤裸裸地鼓吹暴力，难道不是一种无耻？<br />
<br />
中山首里说，“真相正如我所言，他们还是歹人吗？还是恶棍吗？如若真是如此，那么这两人应该就是义士，就是勇士。”我不知道您说了什么真相，我只看到强词夺理、不讲逻辑、无休止的谩骂与攻击。在您心中，可有一丝一毫的谦虚以及对他人的尊重吗？<br />
<br />
尊重别人和自尊、谦虚谨慎的态度、对于真理无止境的追求，本来是现代社会公民应具备的基本素质。可当我看到您这篇指鹿为马、颠倒黑白、穷凶极恶的文章后，我真怀疑您肩膀上长了一颗2010年的脑袋，还是一颗1968年的脑袋！</div>

							
							
							
							
