&nbsp; &nbsp;江泽民究竟出于什么样的意图和动机大规模残害善良的法轮功民众一直是各国政治观察家，普通中国大众乃至&quot;奉令行事&quot;的各级各部官员猜测、谈论的话题。 <br />
&nbsp; &nbsp;　　透过江泽民&quot;在某重要会议的谈话要点&quot;（以下简称&quot;谈话&quot;）以及帮凶打手奉为圣旨的&quot;上面的精神&quot;，我们可以看看这一不同寻常的谈话背后隐藏着什么样的邪恶。 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;1。牺牲民族利益、满足独裁心理 <br />
&nbsp; &nbsp;　　江泽民的邪恶，在于它不惜以牺牲整个民族的前途为代价，再次以挑起大规模群众斗群众的方式来满足树威望、捞资本、搞个人崇拜的独裁心理。 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;&quot;中央鉴于苏联社会主义制度消亡的历史教训，一直决心对各种反马克思主义的思想、信仰和理论进行批判，夺回并巩固无产阶级的思想阵地，在意识形态领域进行一次消毒，法轮功鼓吹‘真、善、忍’，给了我们动手‘消毒’的机会。&quot; <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;──摘自江泽民&quot;谈话&quot; <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;　　镇压伊始，江泽民的喉舌大肆鼓噪，左一个法轮功&quot;破坏稳定&quot;，右一个&quot;威胁政权&quot;，这很给部分国人及国际人士造成了一种江氏虽有点神经过敏、反应过度却也似乎&quot;可以理解&quot;的假象。然而，&quot;谈话&quot;的曝光，使我们可以清楚地看到，江泽民从一开始就非常明白法轮功绝不会威胁任何政权，所谓&quot;夺回思想阵地&quot;、&quot;苏联消亡&quot;的&quot;教训&quot;只不过是掩人耳目的借口。江氏所惧怕的，是脱离了它&quot;精神控制&quot;的民众正在与日俱增，是混乱的社会现实使树立&quot;伟大领袖&quot;形象的希望日渐渺茫。因此，&quot;夺回阵地&quot;的呓语隐藏的只能是江氏图谋达到思想独裁的司马昭之心而已，就如同它对&quot;争夺群众&quot;的渴望所表达的那样。 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;　　首先，江泽民明白：论才干自己腹中空空了无政绩；论资历则平平无奇毫无可取；就连仅有的借以平步青云的那次政治投机也阴为国人不齿。于是，一介宵小出身的投机政客在渴求威望的巨大失落与手握重权身居君位的双重刺激下，为了追求独裁满足它近于变态的自卑自私心理而铤而走险也就是势所必然的。从邓小平死后江氏&quot;现在要好好坐一下&quot;的放言到&quot;谈话&quot;中恶狠狠的&quot;消毒&quot;决心，我们不难窥其心态。 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;　　其次，极度的自卑与怯懦必然导致疯狂的妒嫉和歇斯底里。此恰是&quot;小人得志便猖狂&quot;的真实写照。一个丑陋的东西是容不下任何比它高尚、美好的事物的。法轮功传出短短七年，上亿的弟子相继追随得法，而每一个弟子对李老师发自内心的崇高敬意及为大法舍命而不足惜的坚定曾震惊得江泽民大呼&quot;动员能力之强，组织纪律之高，非常罕见！&quot;。这恰与经历军委开会竟不屑通知他这位不懂军礼常识的&quot;军委主席&quot;此类遭遇的江泽民形成了极其强烈的反差。看看&quot;谈话&quot;中江氏无奈承认&quot;斗争&quot;了一年仍有五千多万法轮功弟子继续信奉法轮功的现实。我们不难理解，镇压法轮功，并以江氏无出其右的凶残对待法轮功学员只不过是被妒火折磨得痛苦不堪的渲泻、转嫁自身罪恶的需要罢了。 <br />
&nbsp; &nbsp;<br />
