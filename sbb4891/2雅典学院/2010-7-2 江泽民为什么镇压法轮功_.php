&nbsp; &nbsp;<br />
&nbsp; &nbsp;──摘自江泽民&quot;谈话&quot; <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;　　从江氏的这段话中，我们不可思议地看到：原来江泽民及其帮凶压根不糊涂，并非&quot;下情不察、妄作决断&quot;的莽撞昏君。它清清楚楚地知道法轮功的大善大忍和与世无争，也清清楚楚地了解法轮功学员&quot;打不还手，骂不还口&quot;的显著特征。只是在它那充满罪恶与邪术的脑子里，如此高尚的准则如此美好的境界触动的不是理应尚存的良知和起码的人性，而是自以为有利可图的贪欲和莫名深深的恐惧。正是在这种邪念的驱使下，江氏动用了所有邪恶的思想及手段选择对法轮功大打出手，作为树立个人威风和建立独裁暴政的机会。 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;　　在江氏眼中，&quot;制造暗杀、毒气、爆炸&quot;的邪教或恐怖组织由于&quot;不容易解决&quot;，可以予以容忍；闹独立、搞分裂并且信仰&quot;反马克思主义思想&quot;的人士由于&quot;工作难度&quot;，甚至可以主动要求&quot;对话&quot;；而尊奉&quot;真、善、忍&quot;为原则，严守&quot;打不还手，骂不还口&quot;的善良民众反倒可以&quot;放手打击&quot;！！众所周知，从来只有强盗匪帮才会从打击好人的恶行中寻找耀武扬威的机会；只有卑劣、怯懦的流氓才会从欺压良善的嚣张中获得&quot;谁敢惹我&quot;的快感。可悲的是身居大国君位的江泽民，对此种下作手段不但不以为耻，反而引以为荣地作为独门心得向众爪牙隆重推销，其道德之卑下，天良之泯灭，当真前无古人，后无来者！ <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;　　江泽民的邪恶，源于它甘愿接受邪恶的摆布，喜欢用邪恶手段达到目的。所以，这样一个充满各种邪恶因素的生命注定是要与&quot;真、善、忍&quot;宇宙大法相对立的。 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;　　江泽民的邪恶，源于它需要一个与其相适相配的不良环境支撑它生存，所以，一个亿万民众身体健康、道德高尚的祥和局面，是江泽民之流发自内心惧怕和不愿出现的。而黄、骗、赌、毒四处泛滥、贪官污吏俯仰可得、道德信仰全面崩溃的乱世，反到是江泽民自感如鱼得水的好去处。 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;　　如果说江泽民因担心&quot;义和团&quot;重演而镇压法轮功，那么当它了解法轮功的情况后会停止镇压；如果说江泽民因一时失去理智镇压法轮功，那么在一次次&quot;构陷&quot;一轮轮&quot;升级&quot;失败后，它会重新考虑如何收场而有所收敛，可惜以上都不是。从江氏明知是错也要干到底的那股子邪劲里，从&quot;谈话&quot;里梦想恶行嚣张&quot;二、三十年时间&quot;的狂语中；从它对死后仍能继续镇压的迫切渴望中，我们再一次看到：这一场人类文明史上最可耻的镇压绝不是基于维护政权，更不是&quot;决策失误&quot;。这是一个本质邪恶注定淘汰的生命企图用疯狂掩饰对正义的恐惧的徒劳举措，是一个邪恶势力的总代表在末日来临之际的最后挣扎。绞尽脑汁的江泽民及其帮凶们什么都想到了，就是忘了一点：邪不胜正是宇宙恒久不变的理。 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;3、江泽民手段歹毒、阴险和无耻。 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;　　&quot;我们公安部和国安部的同志、全国研究语言和宗教的专家，……写出了一些‘经文’，在海内外流传，不断瓦解法轮功信徒的意志，起到了很好的作用，要继续撰写这类‘经文’，扩大法轮功信徒之间、骨干之间的矛盾。&quot; <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;　　&quot;我们对李洪志及其法轮功绝不能掉以轻心，必须加紧各方面的工作，李洪志半年多没露面，大量法轮功信徒仍然死心塌地追随，可见流毒之深，特别行动小组要继续加强行动，设计多种预案，保证刺杀行动万无一失，首恶一死，许多问题会迎刃而解。&quot; <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;──摘自江泽民&quot;谈话&quot; <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;　　自古云&quot;上梁不正下梁歪，中梁不正倒下来&quot;。当堂堂一国之君都热衷于江湖下三滥招数之时，举国上下的邪恶是不难想象的。 <br />
&nbsp; &nbsp;　　借用&quot;XX日报&quot;惯用的手法，既然江泽民等&quot;一小撮死硬分子&quot;是&quot;有预谋、有计划、有组织、有步骤&quot;地发动了这场祸国殃民的镇压，那么种种黑社会匪帮手段的熟练运用也就不足为奇了。 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;　　在江泽民对各部&quot;同志&quot;伪造李老师&quot;经文&quot;的嘉许中，很容易使人想到从《光明日报》开始的众媒介发难，到&quot;香山集体自杀&quot;的谣言，进而到科痞何祚庥的表演，再到近期&quot;十一&quot;大集会的构陷，不过都是江泽民一手自编、自导、自演的闹剧。这种先是贼喊捉贼栽赃嫁祸，进而大打出手充当正义英雄的典型黑社会招数居然被堂堂大国之君拥为看家法宝，真乃天下奇闻。 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;　　先不论一年多镇压过程中一万多人是如何未经法律程序被送进劳教所、500多人是如何以&quot;莫须有&quot;罪名被判刑的；也暂不论50余名修炼者是如何被迫害致死、600多人是如何被送入精神病院进行药物摧残的。单只从&quot;谈话&quot;中江氏妄图陷法轮功于绝境的凶相毕露，痴谋置李老师于死地的杀气腾腾，就可想见这场镇压的邪恶程度。暗杀、绑架、勒索，拐骗、偷抢、暴力，还有精心打造的诽谤以及层出不穷的酷刑，甚至包括在封建时代就已摒弃的株连，一切邪恶的，见不得人的手段，一切黑帮老大的作派、流氓政客的惯技以及一切恐怖组织的拿手招数，邪教魁首的经典科律，无不统统涌现在江泽民四处鼓吹的&quot;中国人权状况最佳时期&quot;！ <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;　　好一个古今邪恶的集大成者，好一个十恶不赦的江泽民。 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;　　&quot;宇宙不是为了人类而存在的，人只是最低下的一层生命存在的表现方式，如果人类失去在宇宙这一层生存的标准，那就只能被宇宙的历史所淘汰掉&quot;。（《再论迷信》） <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;　　江泽民是邪恶的，它以为只要拥有权力与阴谋，便可四海之内随心所欲、为所欲为；它以为只要心肠足够狠、手段足够辣，便可让所有人屈从于它的淫威，敢怒不敢言。 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;　　江泽民是愚蠢的，它不懂得&quot;回头是岸&quot;，不懂得悬崖勒马，不懂得珍惜伟大宇宙留给它的万古难逢的机缘。而如它一样的邪恶败类终究也只不过在一番小丑式的表演后即将成为江氏的陪葬。揭穿江泽民的邪恶是因为宇宙中再不会有它的任何位置；揭穿江泽民的邪恶是愿天下的好人，愿做好人的人不会因它的邪恶而是非不分，从而给自己生命的永远造成无可弥补的损失。 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;　　愿所有善良的人们用你们的良知、正直和正义感支持我们！ <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;　　愿所有善念尚存的人们分清正邪、明辨善恶，给你们自己一个光明的未来！</div>

							
							
							
							
