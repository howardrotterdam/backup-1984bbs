													<h2>一个人的言论是否可以违宪</h2>
						
						
																											<div id="postmessage_645160" class="t_msgfont">邵建 <br />
<br />
　　<br />
<br />
　　一个历史教师（袁腾飞）因其言论而引起网络争议，我无意评论这位教师的言论是非，倒很想就批评乃至围攻这位教师的言论，尤其是其中涉及宪法的言论，表达自己的看法。<br />
<br />
　　最近北京市海淀区那封匿名的“群众来信”很引人注目，我注意到它在指责这位历史教师的言论时，声称他的言论“已经违反我国宪法基本原则”。近日有一视频，一干人众因袁氏讲座（已被取消）到商务印书馆去“踢馆”，在他们义正词严的声讨中，也声称自己是“维护宪法尊严”。就在我写此文时，“猫眼看人”上有一新帖，要与北京政法大学某讲座上的四位教授公开辩论，因为他们的言论“违反国家宪法”。<br />
<br />
　　口口声声是宪法，在任何意义上都是一件好事；但谈宪法要懂宪法。以上指责，概括起来，无非是说言论违宪，但一个人的言论是否可以违宪，这本身就是宪法知识上最基本的问题，恰恰在这个问题上，那些言论违宪的指责者，无法不自陷于法的无知，却又不自知。<br />
<br />
　　今天，世界上任何一个法治文明的国家，都不可能有言论违宪的指控。如果这种指控一旦成立，它本身才是真正的违宪。这里，言论不是不可以违宪，而是不可能违宪。因为言论是一种权利，宪法的存在，就在于保障权利。如果权利是宪法保障的对象，权利在什么意义上可以违宪呢。天下没有这样的逻辑，宪法因权利而存在，却说权利违反了宪法。<br />
<br />
　　然而，权利不可能违宪，并非违宪不可能发生。事实上，违宪的事每一天都在世界各地上演，本土自不例外。那么，到底什么是违宪？根据以上，可以最简单地说，违宪就是违权（利）。就宪法而言，权利如果不可能自己违反自己；那么，可能违宪和可以违宪的是谁呢？不言而喻，就是权力。<br />
<br />
　　言论自由作为个人权利不可能违宪，但如何对待言论自由，政府权力却可以违宪，并可能经常违宪。如果权力以立法的形式限制言论，是为立法违宪；如果权力以行政的方式干预言论，是为行政违宪；如果权力进一步诉诸司法，以刑事方式惩罚言论，则为司法违宪。<br />
<br />
　　由是以观，违宪的主体，具有唯一性。在法治文明的语境中，权利不具备违宪的资格，权力才具有违宪的本能和冲动。落实到以上那位历史教师的言论，你可以认为他错误，就像他也可以认为你错误。但言论上的错误，顶多可以违法（就言论可以伤害他人的权利而言，如诽谤罪），但却无从违宪。推广之，言论之外的一切权利，都可以违法，但都不能指其违宪，否则要闹宪法的笑话。在这里，法是法，宪是宪，层次不同，不能混淆。比较之下，法，大致是针对权利对权利的侵犯；宪，则是专门针对权力对权利的侵犯。因此，正如权利具有违法的可能，违宪则是权力的专利，需要我们随时警惕。同样需要警惕的是，因其观点不同，便以违宪的名义向权力举报，呼唤政府出面解决言论问题（这其实是言论无能的表现）。假如公权就此干涉，这才是违宪，或违宪的开始。（作者系晓庄师范学院教授）</div>

							
							
							
							
