<br />
<br />
　　一些不明就里的人认为，温家宝与李鹏的此番交恶，是因为这两人因「六四」事件结怨，是改革与保守之间的战斗。其实这种说法根本不对。实际上温家宝与李鹏都是「六四」的受益者。李鹏因为站在了邓小平一边而得到保护，温家宝也因为站在邓小平一边，充当邓布在赵紫阳身边的耳目，在关键时候阻止赵紫阳召开政治局会议而得到邓的信任，从而为自己的总理之路打下关键的伏笔。他们都是「六四」的最大受益者。 <br />
<br />
<br />
　　除了前面说的，温总想抢夺电力这个战略行业，是利益之争导致温与李交恶外，还有两个原因也导致温总对李痛下杀手。 <br />
<br />
<br />
　　一是温知道李鹏身披「六四」血债，打李鹏会得到天下人的支持。所以温总才要千方百计地利用各种机会，把自己打扮成一个民主派、改革派。内部人认为，不管李鹏家族多么贪腐，若论中国30年改革，做总理的，第一个首推赵紫阳，关键性起步都是赵设计并进行的。第二个，大概还得算是李鹏。李鹏虽然没有多大的才能，但他一是稳得住盘，二是能容人能用人。确保经济改革之路不中断，没有退到左的一边，李鹏还是立下功劳的。朱F基那么大的毛病，他都能用，田纪云等几个副总理的能力也都不弱，改革开放能平稳坚持下来，李在台上时的「无为而治」，虽无大进，也无大错。在改革盘桓期，不退就是进。再过来，就是朱F基了，脾气大，毛病多，但敢做敢为。分税改革强化了中央集权，虽造成国富民穷，但也为国富民富创造了条件。最不行的，就是这温家宝了。八年任内，除了减免农业税一条，搞了什么？就是这一条，也是托赵、李、朱的福，攒下那么多银子。贫富差距进一步扩大，跟八年前相比，大陆百姓是更看不起病，更买不起房，更上不起学，基尼系数远超国际警戒线，弄得中华大地民怨沸腾，有怨无处伸已到了被迫杀童警世的地步。除了做秀技术高超，温总还有什么？ <br />
<br />
<br />
　　二是一些左派对温总不满，在网上写文章批评温总。温总认为李鹏是这些人的背后推手。海外那些关于温家腐败的消息，温总也怀疑是李鹏那边的人放的风。海外网站有一篇挺温的文章《温家宝的天安门广场在哪里？》就说：「凡是温家宝讲话过头，或者想有所动作的时候，网络上马上就及时公布他儿子和老婆贪污腐败的事。」「正如21年前，当时赵紫阳的儿子经商被揭露出来」，「而透露出这个消息的，包括陈云与李鹏的部下」。矛头直指李鹏。 <br />
<br />
<br />
　　一位电力行业人士透露，三峡大坝近来出现很多裂缝，库区时常出现山体滑坡，生态环境恶化，大量水藻累积，大坝至重庆间库区有近万处危险点，据称30万人受波及需要迁移。但温李大战期间，中央领导没有人敢过问此人命关天的大事。原因很简单，包括身为党政军一把手的胡锦涛，一直努力不愿将自己与三峡扯上干系，即不愿为李鹏去冒险，也不愿得罪温家宝。温家宝不按常理出牌，已经令其他常委头痛兼胆怯。李鹏那么大的家族事业，短短几年就姓了温。有这个前车之鉴，谁都害怕不小心招惹了温总。 <br />
<br />
<br />
　　该人士还透露，据温总身边的人说，温家宝对三峡这个争议工程的基本态度是，若能赚钱，就继续赚，反正三峡是个金口袋，多少钱都能装进去，多少钱也能洗出来。如果实在不行，该顺应民意时，该拆就拆，反正有李鹏在那儿担责。 <br />
<br />
<br />
　　经此一役，李鹏家族虽谈不上完败，但温家宝却是大胜。通过胜李鹏而警告天下，温旗所至，躲的躲，怕的怕，服的服，党内再无象样力量可以制衡温家宝。剩下一个胡锦涛，屡屡在关键时刻被传中风，无论真假，都说明他已铁定成为中共建政以来最弱君主, 无任何威严可言。只能眼看着温家宝上窜下跳而无可奈何。试想，十八大布局正在展开，一个心智不健康的总书记，一个马上要下台的总书记，有点实力的，谁还会去投靠？谁还会死吊在他那棵树上？ <br />
<br />
<br />
　　而已经过气的江泽民也被屡传「健康问题」，甚至住进了301医院。这样的消息屡屡传出，对谁最有利是不言自明的。在这样的情况下，党内的第一实权人物，其实就成了温家宝。温家宝不仅收获了巨大的经济利益，也成了共和国历史上最有实权的总理，十八大话语权，温总已经紧紧地纂在手心里了。 <br />
<br />
