													<h2>借势六四日记  “双血统”温家宝成最有实权总理</h2>
						
						
																											<div id="postmessage_722318" class="t_msgfont">最近，李鹏「六四」日记近日弄得沸沸扬扬，各方猜测与分析莫衷一是。据中南海内部人士透露，中共高层已将该书泄露视为重大政治事件。到底是李鹏自己，还是他的政敌泄露出去的还不清楚，但该书「适时」泄露背后却大有文章。 <br />
<br />
<br />
　　相关消息称，李鹏「六四」日记本拟在大陆公开出版，但中共高层有人不同意，认为时间敏感，于是只印成内部资料叫《关键时刻》，在一定范围内发放。中南海内部人士透露，李鹏之所以在中共高层内部抛出「六四」日记，最重要的原因有三点： <br />
<br />
<br />
　　一是李鹏来日无多，要做一些政治交待。 <br />
<br />
<br />
　　「六四」是李鹏最大污点，在自己死前，要把当初一些内情交待出来。澄清一些他认为重要的事实，至少在他看来，开枪是邓等一些元老的指示，是中共集体决定，不是他一个人的责任。李鹏书中将责任主要归之于邓小平。 <br />
<br />
<br />
　　李鹏在《关键时刻》中为自己辩护，撇清责任，指1989年5月17日，由邓小平拍板决定军队入城及北京实施戒严，当时军委副主席杨尚昆也在场，只有赵紫阳反对决定。并指他自己亦已作最坏打算，准备好牺牲自己及家人的性命，阻止示威变成好像文化大革命一样的悲剧。李鹏在日记透露，在5月19日，邓小平指出次动乱出在党内，直言「中央有两个司令部」，表面上是李鹏和赵紫阳，实际上是他自己和赵紫阳。邓提到戒严时指「如果我们提出绝对不用杀伤性武器，那是不行的」。果然，日记一流出，香港媒体就有评论认国，邓是主凶，李只是帮凶。 <br />
<br />
<br />
　　二是向温家宝示好，为温总「六四」中的行为辩护，替温总回应党内左派的批评。 <br />
<br />
<br />
　　近年来，对温家宝的批评声越来越多，尤其是中共党内的左派，对温发起了一轮又一轮进攻。比如，温家宝提出「普世价值」，以中国社会科学院院长陈奎元为首的一帮左派，就公开撰文批评「普世价值」。再如，去年9月，曾任中央政策研究室局长的左派代表人物张勤德撰写万言书《对温家宝总理的六条意见》，在中共内部传播，对温家宝当政以来的政策进行了无情的批判。文章历数温家宝六大「罪状」：中国特色资本主义思想得到更大解放、资产阶级自由化改革观产生更大影响、庸俗发展观造成更大危害、卖国求荣的犯罪行为更严重地发生、赵紫阳集团的气焰更加嚣张、人民群众的根本利益受到更大损害，等等。除此之外，还提出关于温的太太与儿子从商的各种传言，要温总澄清。这篇文章后来被人弄到网上，引发的讨论至今未息，使温家宝十分狼狈。 <br />
<br />
<br />
　　究其源头，多年来，左派一直将温家宝陪同中共总书记赵紫阳在天安门广场看望学生的行为，视作温家宝是赵紫阳集团的「余孽」证据。「六四」后，中共左派元老薄一波、王震等也因此事向温家宝发难，认为其不合适继续在中央工作。李鹏日记中多次提到温家宝，展示了一个与左派眼中温家宝是赵紫阳集团代表人物形象完全不同的另一面。李鹏除了用一些事实证明了温家宝坚决站在党中央、邓小平一边，甚至称赞温家宝「党性原则很强」。如关于温陪赵去天安门一事，李在日记中就明确记载为：「凌晨5时许，由中央办公厅统一安排，赵紫阳由中办主任温家宝陪同，我由国务院秘书长罗干陪同，分乘两辆救护车去天安门广场，看望绝食同学。」肯定温陪赵只是中央决定，是工作内容，而非私人帮派行为。李鹏还提及宣布戒严之后，温家宝积极表态，表示「坚决拥护中央的方针，拥护小平同志的决定，坚守岗位，努力工作，积极完成中央交给中办的各项任务」。还透露，戒严令宣布之后，以彭冲、叶飞为首的一批全国人大副委员长要求委员长万里中止访问美国，回国主持紧急会议，赵紫阳也口头表示同意让万里中止访问美国，政治局常委胡启立要温家宝发电报通知万里，但温家宝「看了电报稿之后，就对胡启立说，这个电报中办不能发」。证明关键时刻，温家宝同志决定地站在邓小平一边，李鹏为此称赞温家宝「做 得是对的」。直接证明温家宝在整个「六四」期间，始终以党性为原则要求自己。 <br />
<br />
<br />
　　这也与赵紫阳回忆录中的相关记载相印证。赵在回忆录中说，是温家宝在关键时刻阻止他召开政治局会议的想法。赵的家人对温家宝也是「没有什么好说的」。 <br />
<br />
<br />
　　三是间接告诫中共高层，他身上有两个「血统」。一个是「革命的血统」，一个是「六四的血统」。正是他这个革命者的后代，是他李鹏在关键时刻保卫了党，才有了大家的今天。 <br />
<br />
<br />
　　李鹏在日记中，再三强调自己在关键时刻为了「维护党的利益」、「维护党的团结」，而忍辱负重，与「分裂势力」「坚决斗争」，才有了今天的局面。言外之意，没有他当时的坚决，没有「六四」开枪，不仅没有今天「改革开放的大好局面」，更没有什么江泽民、胡锦涛和温家宝的今天。 <br />
<br />
<br />
　　网「《李鹏日记》原是为李小鹏隔代接班呐喊助威」一文中有一点说的很对：李鹏使用这本书向全党表明：是他李鹏当时的坚定不移，才保住了共产党，才有了今天共产党成为最大的利益集团，儿孙子女享受荣华富贵。究其本质，江泽民、胡锦涛、温家宝，跟李鹏一样，「六四」的最大受益者，大家的权势、钱财，无不来自于「六四」。如果有利益了大家只享受利益，却把鲜血全洒在李鹏一人身上，那就是这些人的政治品格有问题。 <br />
<br />
<br />
　　也即，李鹏通过日记客观上展示了一个「六四的血统」，现在的中共高层，都是这个「六四血统」的产物，享受着「六四血统」的既得利益。若有人既想当婊子又要立牌坊，不光李鹏不答应，中共也不答应。 <br />
<br />
<br />
　　李鹏敢以此提醒中共高层，也是因为他多年来，对军队利益的照顾在起作用。目前中共三军高级将领中，受到周恩来恩宠的高级领导人的子女掌握了绝对的军权，基于历史血统，这些人对共产党和李鹏都有特殊感情。这是「革命的血统」所决定的。 <br />
<br />
<br />
　　如此身系两大「血统」，谁想挑战李鹏，就要掂量掂量了。 <br />
<br />
<br />
　　果然，李鹏日记一传出，李小鹏的行情就看涨，最近荣升山西常务副省长，就是这两个「血统」在起作用。 <br />
