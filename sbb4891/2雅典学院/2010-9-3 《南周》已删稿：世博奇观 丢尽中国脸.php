世博奇观，丢尽中国脸（南方周末本期世博专稿，原文已被删改）<br />
<br />
有此一说 2010-09-02 23:04:21 阅读9280 评论7&nbsp; &nbsp;字号：大中小 订阅<br />
<br />
(《HIGH过之后，优雅起 来，世博开启国民素质成人礼》初稿)<br />
<br />
这篇稿子历经七次修改，数次送审，最后出来的稿子已经与原文完全不同，所有批评世博会的内容悉数删除。<br />
<br />
世博固然是一场盛会，但它只是一个国家成长历程中的一 个节点，我们记录这期间发生的尴尬，并不心怀恶意。<br />
<br />
所谓的“文明”从来指的不是老百姓是不是随地吐痰、大小便，而应是生活在其间的公民如 何自主地改造生活，如何生活得更加从容、克制、理性而有尊严。<br />
<br />
探讨文明现象，不代表我们用柏杨式的文章对国人“横加”指责，我们更愿意由此展开对一 个城市乃至一个国家治理方法论的探讨。<br />
<br />
直面尴尬，心怀美好，改变随时随刻可以开始。<br />
<br />
 <br />
<br />
作者：陈鸣、实习生 刘高阳<br />
<br />
 <br />
<br />
 上海世博会，又一个属于中国的时间。在展现城市文明的同时，这 个横跨浦江两岸的巨大园区本身就是一个存在184天的微型城市。游客将与现代场馆共同构成这次展示。<br />
<br />
“城市让生活更美 好”，然而，行走在这个临时城市里的游客却正在让这个系统濒临崩溃。<br />
<br />
 <br />
<br />
失控的“热情”<br />
<br />
在经历了最初的好奇之后，很多外国场馆工作人员对中国的好感被冲刷殆尽。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 两个月前，阿娜斯塔西亚（Anastasia Yevets）对上海世博会充满了期待，而如今，她的心情却比上海连绵的梅雨更加糟糕。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;这个来自白俄罗斯的姑娘和她的同事们正在目睹一场“灾难”的发生――他们的场馆越来越像一个动物园。中国游客们在场馆里大声喧哗，接打电话，拿起相机对 着每一个金发碧眼的老外狂拍。“有时候，中国人一手猛拍着桌子，一边对你喊着嘿！嘿！嘿！他们想喊我合影，却让我觉得自己像只被围观的猩猩。”最令阿娜斯 塔西亚难以置信的是，几天前，一位中国老太太甚至放任她的孙子在场馆的正中央拉了一坨大便。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;处于震惊和崩溃边缘的并不仅仅是阿娜斯塔西亚。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;在古巴馆，Shela Borges Glez见到了令她不解的一幕。一开始，她们在墙上划出一小块区域供游客留言，但是，只用了两天不到的时候，汉字 就像蝗虫一般疯狂地覆盖了整个场馆的墙壁。中国游客的热情程度显然令这些加勒比海居民猝不及防，甚至连Shela的办公室都不能幸免，在玻璃门上，中国人 写满了“XX到此一游”和“XX我爱你”之类的字样。在数次清理之后，Shela和她的同事放弃了努力，每次擦完不久，中国人又将攻占他们的玻璃。他们只 好贴出“禁止往墙上涂画”的告示，以这样的形式为自己一开始的错误决定埋单。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 世博园里发生的这一切，就上海的雨季一样，无休无止，令人心生厌倦。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;埃及馆的Tahany刚刚从开罗飞到上海来接替她同事 的工作，那位好心的前任馆长提醒她“一定要看好我们的石头！”场馆里摆的雕像都是公元1000多年前的文物，其中包括有“阿蒙霍特普四世的巨像”和“爱神柱”之类的珍品。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 这些从开罗运送过来展品大部分没有加装防护罩，“因为在我们国家没有人会去摸文物，因为那是一种犯罪行为”，但很快，埃及馆的大部分工作人员不得不放弃原先安排的工作，他们每两个人守着一处雕像，并拉上围栏，阻止每一只靠近的手掌。一个中方工作人员告诉南方周末记者，埃及人迅速地学会的第一句汉语不是“你 好”，而是“不要摸”，每天念咒般地重复上百遍。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 在捷克馆，游客们一度像苏联红军攻占柏林德国国会大厦一样纷纷爬上圣约翰・波穆克的青铜像，直到铜像被护栏围了起来。在孟加拉馆盖章，一位黑人工作人员面 色冷竣，机器人一般地只重复说一句话：“排队，排队，排队，排队……”。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 在经历了最初的好奇之后，很多外国场馆工作人员对中国的好感被冲刷殆尽。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;“他们刚到上海的时候，看到那些宏伟奇特的展馆，都是‘Wow’的惊叹，觉得中国人很了不起，而在见识了中国游客之后，所有人都开始讨厌中国人。”和那 些面对人山人海的中国不知所措的朋友们不同，阿娜斯塔西亚算是半个“中国通”，她有一个中文名字叫做“梅芳”。然而在学习汉语三年以后，她突然发现不知该 如何向自己的朋友解释这样一个复杂的中国。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 在梅芳的朋友里，很少有人像她一样游历过中国贫穷的乡村和小城镇，更不用提理解眼前这些操着各类不同口音的人做出种种“失礼”举动的原因所在。“我经常告 诉朋友们，不是所有中国人都是这样。”<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 但即使是梅芳，也很快失去了耐心，她的眼睛在无数次突如其来的闪光灯照射后开始干涩胀痛，有一天因为制止游客插队，一个中国男子用地图狠狠地拍打了梅芳的 手臂，这次袭击让她委屈得哭了起来。在中国小孩在场馆中央大便之后，白俄罗斯馆终于用巨大的围栏把中央的圆形场地围了起来。<br />
<br />
 <br />
<br />
 守不住的场馆<br />
<br />
 作弊与反作弊，破坏与反破坏，偷盗与反偷盗，成了一场每日上演的拉锯战。<br />
<br />
 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 当“梅芳们”在场馆里焦头烂额的时候，蔡雯俊和她的同伴们也在人潮中努力站稳。她来自华东政法大学，是世博文化中心的一名志愿者。很多时候她被调配到北门疏导人流，在这个出口，人群被分成两队，一队通往六楼参观，一队则直接出馆，两个队伍中间用隔离带隔开。很多时候隔离带和志愿者的引导被视若无睹，只有在 发现走错了之后游人才返过来要求志愿者让他们原路进馆。“他们既想不受控制地乱走，又丝毫不愿承担走错的后果。”<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;连不明国情的外国工作人员也很快明白，“过度热情”已经无法解释眼前发生的一切。除了不讲礼貌和规则，更多千奇百怪的状况从第一天开始，从场馆的入口处，就层出不穷――一位法国馆的工作人员告诉记者，最初的几天他们像目睹奇迹一样，看着那些从绿色通道坐轮椅进入的游客一进馆就纷纷站立起来行走。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 破坏规则的好处显然十分诱人――当别人还在排两三个小时甚至更长时间的队时，绿色通道上的人员却可以在二十分钟内进馆，而且还能捎进一个“护理人员”。在 一些场馆门外至今可以看到为数甚众、真假莫辨的残疾人排起长队。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; “有一次，一个有着强壮肱二头肌的中年男子坐在轮椅上，很疼似的紧紧握住自己的手臂，却告诉我们他患的是小儿麻痹症。”一个在沙特馆门口协助维持秩序的志 愿者说。一些明显已经接近10岁的孩子被父母安排坐进了婴儿车，以此换取不排队的特权。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 在经历了最初阶段完全开放的姿态之后，很多场馆开始小心调整收缩他们的策略。所有试图由绿色通道进馆的老人、孩子和残疾人都被要求出示相关的证件，一些明显有作弊行为的游客被挡在通道之外。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 但这只能挡住一部分人，精明的游客们依然能够掏出各类红绿缤纷的证件。有的家庭为了让一家老少都能通过，在场馆门口精心商量着如何分配手头的老年人身份 证、残疾证和婴儿车。这看起来像是一盘跳棋游戏，任务就是一堆棋子搭上另一堆棋子的顺风车，从而跳到场馆的那一边去。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 即使作弊失败，对大多数人来说也没有什么损失，“通常被戳穿了之后游客们还一边大骂工作人员，一边离开，脸上不但没有惭愧，反倒对我们有不通融办事的鄙夷”，一位来自江西科技师范学院的工作人员愤怒地说。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 进入场馆内部之后，游园狂欢才刚刚开始。在捷克馆，一个名为“捷克明珠”的视觉装置吸引了游客围观，这个由5个巨大玻璃面组成立体屏幕璀璨明亮，令观众仿 佛触手可及。开馆之初就不断地有游客用脚磕打玻璃，他们只是想知道屏幕到底是不是玻璃做的。于是一个专门的工作人员被派在此处专门制止人们的破坏行为。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 在摩肩接踵的嘈杂环境里，偷盗与反偷盗，也成了一场每日上演的拉锯战。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 在泰国馆的最后一个参观环节是放映一部3D的电影，场馆内的设施会随着电影场景洒水和喷施香气，显然十分钟的尖叫体验令很多观众沉醉其中，于是，他们决定 趁混乱把3D眼镜捎回家去。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 泰国馆的解说在每场电影结束时都会向游客们恳求：“这个眼镜在家里看电视是不会有3D效果的，我们的眼镜已经越来越少，请您不要拿走。”但结果令人无奈， 泰国馆馆长Saranpat Anumatrajkj向南方周末记者抱怨，馆内的3D眼镜每天以5%－7%的速度丢失，“每放一场电影大约会损失10个， 而我们每天大概会放50多场电影。”<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 在中国铁路馆，3D影院的200多个座位如今只能坐100多人，原因是3D眼镜被偷到只剩这么多了。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 香港人似乎对3D眼镜遗失的可能性有更充分的预判，他们直接在眼镜上贴条形码，在场馆出门处设立安检门，任何试图带眼镜离开的人都会引起机器的尖锐叫声。 尽管如此，人们并没有放弃努力，安检门因此每日鸣叫不已。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 比眼镜更小的物品更适合被带走，在波黑馆，狭长走道上的液晶电视上的8GU盘被游人尽数拔光，最后馆方不得不把数据盒藏起来，通过USB线再连接到电视 上。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 也有人尝试更有挑战性的目标，6月27日下午，波黑馆的两名中国游客从严密封闭的玻璃壁橱里成功地偷出几件嵌有珠宝的首饰，没有人知道他们是怎么做到的。 幸运的是另一位游客在离馆前及时地举报了他们。<br />
<br />
 <br />
<br />
盖章族的围攻<br />
<br />
 “他 们做的就是盖章盖章盖章盖章……”中国人自己打了起来。打斗密集地持续了一两天后就没再发生，因为印章也被偷走了。<br />
<br />
 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;“世博会的主题曲应该是一片笃笃笃的敲章声。”突尼斯馆的Anis Basti调侃道。世博护照，这个起源于1967年蒙特利尔世博会的玩意儿从来没像在中国这么火爆过，这令他根本无法理解。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 围绕世博会赚钱的黄牛们开始提供盖章服务，游客只要在门口等待，黄牛入馆盖章，最后收取一笔数十元的手续费。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 场馆有冷热门之分，而在盖章这一事情上，每个国家的场馆终于实现了“平等”。在只有6个房间的乌拉圭馆，为应对盖章的人流，其中一半房间被改成了盖章专用 房。在泰国馆，铜、木、橡胶等各类材质的印章则被悉数敲坏。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 中国游客的狂热在盖章上终于全面爆发。在丹麦馆，游客为争夺印章而与工作人员发生冲突，这被拍成视频传到了网上。爱尔兰馆的印章被当场抢去，原因是工作人 员拒绝为同一个人盖数十本护照，于是游客决定自己动手，最后爱尔兰工作人员不得不报警。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 拒绝这些长时间排队的中国游客的盖章要求时常要冒一定风险。“死爱尔兰鬼”、“死丹麦鬼”、“死法国佬”……各种口音的咒骂足以令人崩溃。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; Shela所在的古巴馆干脆将盖章台移到场馆外面，并用铁链将章子链了起来。更多场馆把印章收了起来，不再提供盖章服务。在白俄罗斯馆，工作人员同样挂出 了纸牌，上面直截了当地写着“没有章”。“他们做的就是盖章盖章盖章盖章，根本就不看我们的馆”，一位挪威馆的工作人员在面对上海电视台的摄像机时忍不住痛哭。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 泰国的Saranpat告诉记者，盖章的人群不时地与工作人员发生冲突，最后他们干脆把印章放在桌子上让人们自己动手，导致的结果却是更加混乱，中国人自 己竟然也打了起来。打斗密集地持续了两三天后就没再发生，因为印章也被偷走了。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; Saranpat很不理解，他们的场馆设计用很多技术手段设计了逼真的互动体验，依然有很多中国人进来只是盖个章，然后就匆匆寻找出口。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 对这一点中国人看得更清楚，一位经常出入世博园的上海本地记者说：“其实世博会更像一个充满异域风情的游乐园，人们来这里玩耍，心态和旅游没有区别，那些代表现代科技走向的技术既没人懂，更没人看。”<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 而“盖章”则不一样，这在中国从来不是一件简单的事，有时候甚至是一种文化。有一次，白俄罗斯馆要送一个平板小推车进园区，最后盖了十多个章才获得批准。 每一次填写表格，都是疲于奔命地在楼上楼下四处寻找那些手握印章的人。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;“这是一个很奇怪的社会系统，所有人在找人办事。在我们国家，一个章就足以通过所有审批。”梅芳无奈地说。<br />
<br />
 <br />
<br />
 <br />
<br />
失去尊严的地方<br />
<br />
 一群筋疲力尽的排队游客终于情绪失控，他们在场外齐声大喊“纳粹！纳粹！”，以致于德国馆不得不 向园方要来了更多的保安。<br />
<br />
 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;每一天发生的零星不快，并不足以阻止游人的到来，相反，在开园初期遇冷之后，每天游客数量都在节节攀升。每天游览人数已经从最初的每天20几万人上升到现 在的45万人以上。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 顾晓芳是江苏一家国企的员工，6月初她被单位组织前往上海参观世博。在人满为患的沙特阿拉伯馆，她看到很多散客因为长达5个小时的排队时间而瘫坐在地，这 个过程中大人们没有机会上厕所，小孩子们开始随地大小便，有的人开始打牌玩游戏机，更多人无所事事。这样的场景让她想起了恐怖的春运。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 闷热潮湿的上海加剧了人们的烦躁。上个月德国媒体报道，一群筋疲力尽的排队游客终于情绪失控，他们在场外齐声大喊“纳粹！纳粹！”，以致于德国馆不得不向 园方要来了更多的保安。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 顾晓芳认为网络上把园区里出现的种种不文明现象都归结到游客身上并不公平，在她看来，超长的排队使人们失去了尊严。“园区可以修得很大，蛇形栅栏可以设置 得很弯很长，人们的耐心和体力却是有限的。”<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 在志愿者蔡雯俊看来，很多时候园区和游客需要共同改进，比如世博文化中心的6楼有几家餐厅，前来就餐的人可以由餐厅人员带领直接上楼，但是很多人并没有途 径可以订餐。蔡雯俊和她的同伴做过尝试，他们试了包括查号台在内的各种方法都没有找到订餐电话。“我们都联系不上的话，游客更不可能了，最后他们肯定就要 开始抱怨。”<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 来自上海第二工业大学的刘永生是该校志愿者在中国馆的带队老师，她认为在排队两三个小时后人会极端疲惫，场馆方在设置上可以更加人性化。她举例子，在中国 馆南广场排队的游客通常比较长，平时尽量安排到伞亭处，如果遇到天气炎热的情况，就把队伍再往里放，一直排进手扶电梯，“让游客觉得毕竟是进馆了，这样就 可以很好地安抚他们的情绪。”<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;然而陆续增加的旅游人数，无疑还将对园区公共服务构成持续挑战。公共汽车站大部分时间人头攒 动，尤其是世博大道线上的乘客时常需要分流到过江线上，再从就近站点步行到达场馆。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 就在顾晓芳参观世博园前后几天，世博局发布的旅行社团队入园预约情况信息显示，6月4、5两天的预约团队数共达到9152个,预约游客人数共达35.35 万人次。截止5月底，世博局票务中心提供的数据，已售出的3771.2万张票中有接近三分之一是团体票，而其中大量是单位采购的“福利票”。到上海看世博 在很多地方更像是一项任务。一位在重庆工作的女士因为害怕拥挤的原因拒绝参加世博，而被所在工作单位罚款1500元。<br />
<br />
 <br />
<br />
 <br />
<br />
中国成人礼<br />
<br />
 世博会不止是拍个照，盖个章，不理解世博真正的含义，无以理解现代化<br />
<br />
&nbsp;&nbsp;<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 在园区的许多角落，游客似乎正在让世博会走向尴尬。在靠近美国馆的吉野家，欧洲广场附近的肯德基餐厅，在公交车站，不时可以看到冲突的人群。人们因为碰撞、排队、踩脚而爆发了各类口角。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 成年人们看起来兴奋而富有激情，同时像小孩一样易怒。他们中的很多人从周边的江浙一带赶来上海，心情迫不及待，然而从每天早上排两小时队进园开始，这一天 的游览就注定是一个极费体力和耐心的工程。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 阵雨不时地袭击人群，在那些拥挤的队伍里，撑伞的人们互相把雨水滴到周围人的身上，然后开始了互相指责。6月27日早晨7点钟开始，南方周末记者体验了一次两个半小时排队入园的经历。游客的争吵从未停止。两位来自香港的游客在队伍中为人劝架，他们对园方表示不解：“明知道每天队伍都这么长，上海最近又每天 下雨，为什么不多盖一些遮雨棚？明知道每天好几万人等着入场，为什么不把开馆时间提前？”<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 精心建设对比走马观花，在世博园这个微型城市里，人与城市的不匹配成了目前为止最大的尴尬。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 在志愿者蔡雯俊看来，世博会让很多人聚焦在一个空间里，很多问题就会放大凸现出来，这并不是世博会的问题，而是人的素质问题。在大部分行色匆匆的游客的理解里，花160元人民币进园就是享受服务的。在应聘上海世博志愿者时，蔡雯俊曾经怀揣着不错的预期，“我们对游客提供了帮助，他们应该会是感谢的态度，结 果他们经常喊我们服务员！”<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;在集中逛了几个热门场馆之后，顾晓芳和几位单位同事便放弃了继续参观，“说实话，都是看个热 闹，什么高新科技真的看不懂。”他们围坐到高架步行桥下一边打牌一边抱怨这里一点都不比普通游乐园好玩。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; “四十年前的日本大阪世博会参观者突破6000万人，日本一半的国民参加并见证了那场盛会，其成功的举办被公认为是日本国家现代化到来的标志。而上海世博 会的价值正被低估，一个本来应该是全民科技的盛会正在成为一个的只是养眼的景点。”一家上海媒体评论说。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 6月26日傍晚，梅芳坐在白俄罗斯馆的角落里，这份工作已经让她精疲力尽。她开始怀念在北京的生活，她回忆起北京奥运会时候的愉快经历，“所有事情流程顺畅，每个工作人员会讲英语，没有观众会袭击你……”<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 有时候她也在沮丧中表达了理解：“我知道你们有过三十年的封闭，人们的价值观被推翻并重建，就像苏联时候的我们一样。”这似乎正是她理解中国人行为的机缘 所在，面对她那圈已经厌烦中国的朋友，她说：“如果只到了上海和北京，那你就没有真正到过中国，更多的地方不发达。这里有坏人也有好人，有时候他们只是因为还不富裕。”<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 这一天傍晚，在雨水和排队双重考验下，游客们已经筋疲力尽，人群东倒西歪地坐在路边的草地上。一场盛大的花车游行让他们重新兴奋起来。那是一场连绵细雨之后的移动演出，远处是吹奏着西洋乐曲的铜管乐团。近处的一辆花车上京剧演员们京韵婉转，围拢而来的游客们爆发出震耳欲聋的欢呼声，在雨后的阳光下，演员们 水袖挥舞，华装闪亮，那样的场景宛若盛唐。</div>

							
							
							
							
