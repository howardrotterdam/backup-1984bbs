<br />
　　而事实上，远在去伯克利之前威尔斯就已立志反进化论，是出于邪教信仰，而不是科研中的“良心发现”。这是一个已经精心准备了20年的阴谋。在统一教的网站上，有一篇威尔斯在获得伯克利的博士学位后不久撰写的文章《达尔文主义：为什么我去读第二个博士学位》，说得很坦白。统一教是韩国人文鲜明创立的一个教派，在美国被普遍认为是邪教，威尔斯是该教派的虔诚教徒。在文章中，他回忆说，他于1976年在统一教神学院进修两年，“父”（即教主文鲜明）告诉他们达尔文进化论是邪恶的代表，“父的话、我的研究以及我的祷告使我相信我应该献身于消灭达尔文主义，就像我的许多统一教同仁已经献身于消灭马克思主义一样。”<br />
<br />
　　1978年，威尔斯被文鲜明派到耶鲁大学读博士，他把这视为在为挑战达尔文主义的战役做准备。他在耶鲁读的是神学，为了能够“理解达尔文主义与有神论之间的冲突的神学基础”。毕业后，他觉得如果能以科学家的身份来挑战进化论的话更令人信服，于是在1989年又到伯克利读了第二个博士，这回读的是生物学，研究蛙的胚胎发育，因为他确信胚胎学是“达尔文主义的致命弱点”。<br />
<br />
　　我不太明白伯克利生物系为何会招收这么一位既没有任何生物学基础又动机不纯的神学院毕业生读博，而且虽然威尔斯读博期间的科研成果乏善可陈（只以次要作者发表了两篇论文），还是被授予了博士学位。之后威尔斯又留在伯克利做了5年博士后研究（同时兼任美国反进化论的智囊机构发现研究所的研究员），号称研究的是“发育生物学”，然而没有经费，也不做实验，没有发表过任何学术论文，其“研究成果”就是《进化论的圣像》一书。<br />
<br />
　　此书力图证明生物学教科书中作为进化论证据的例子（他称之为“进化论的圣像”）是错误的、误导人的，生物学家在做伪证。对该书进行具体的驳斥不是这篇短文的任务，我此前已在多篇文章中对这些例子做过详细的分析，证明做伪证的是神创论者。在威尔斯的书出版后，有众多美国生物学家――包括伯克利的生物学家――都撰写书评对书中的谎言、谣言一一做了批驳。例如，生物学家布鲁斯・格兰特分析了威尔斯是如何有意歪曲地引用格兰特本人的研究成果的，结论是：“基本上，他是不诚实的。”<br />
<br />
　　和威尔斯的不诚实一样令人惊讶的是他对生物学的无知。伯克利的读博生涯看来并没有让他的生物学知识有什么长进。在他的书中处处可见低级的生物学常识错误，他对其“专业”――胚胎学――也所知甚少，虽然他时时不忘提醒读者他是一名“发育生物学家”。他在书中曾傻乎乎地质疑道：“如果我们的发育基因与其他动物是相似的，那么为什么我们生下来的是人而不是果蝇？”我想，任何一名合格的生物系本科生都会想出一、两条理由来回答这个愚蠢的反诘：相似基因的不同组合会导致非常不同的结果、基因调控的细微差异会导致重大的改变、基因的表达受到环境因素的影响，等等。<br />
<br />
　　当然，威尔斯和其他神创论者一样，无意与生物学家做严肃的对话，生物系学生也不是他们的读者对象。他们前赴后继出版这类谣言大全的用意，在于为神创论者壮胆打气和蒙骗不具有辨别能力的外行读者。名牌大学生物学博士的头衔还是很能吓唬人的，这正是威尔斯要去混一个伯克利博士的用意。</div>

							
							
							
							
