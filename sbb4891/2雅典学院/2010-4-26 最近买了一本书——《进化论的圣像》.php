													<h2>现在简要介绍一下此书各章概要</h2>
						
						
																											<div id="postmessage_586771" class="t_msgfont">序和导言不说了。总的来说就是批判今天的生物学课本（从中学到研究生）还把以下经典进化证据中存在的问题隐藏起来。<br />
第二章――米勒-尤里实验：<br />
这个实验大概没听说过的没有几个了吧。现在问题是地质学家认为那时候因为水蒸汽在阳光照射之下会分解成氧和氢，所以原始大气里面已经含氧，而在有氧条件下米勒的实验绝对不可能产生氨基酸。并且那个时候的大气中也不应该有氨气，这个也是产生氨基酸的必要条件。<br />
第三章――达尔文的生命树：<br />
这个我也不介绍了。受质疑的主要原因就是寒武纪大爆炸。其实主要的反对者是古尔德，本书作者基本照搬古尔德的意见，大致上就是说一开始生命的门类是极度扩张的，随后历经多次大毁灭删除大半变成今天这样的自然界，并且生存与毁灭的决定因素是运气。<br />
第四章――脊椎动物的同源肢体：<br />
这一章主要讲的就是“同源器官――共同祖先”之间的循环论证问题，捎带的证据就是同源肢体不受共同的基因段控制。<br />
第五章――海克尔的胚胎：<br />
这个胚胎也很著名，我也早就听说过这个图有作弊，只是不知道具体细节。大致的情节就是海克尔把胚胎发育的中段当成早期（其实他应该从卵细胞开始分裂的时候开始观察），并且把本来不是那么相似的胚胎搞得几乎一样，还造谣说有两个胚胎标本忘了贴标签，搞得无法分辨等等。本书作者揭露的是这些动物的早期完全没有任何相似的地方，到了中期稍有相似，但还是区别不小，到了后期就不用提了。区别大小的变化大致上类似于一个沙漏的形状。作者在此再次质疑同源器官与共同祖先的逻辑关系。<br />
第六章――始祖鸟：那遗失的环<br />
主要谈到古生物学界对鸟类起源的争议，而课本仍然信誓旦旦地说始祖鸟是鸟类的祖先。<br />
第七章――胡椒蛾<br />
这一章主要讲到19世纪胡椒蛾实验过程及结果的问题。一个是胡椒蛾并不栖息在树干上，而是主要栖息在树枝下。其次是论述英国开始强制性环境保护之后，英国境内原来变黑的胡椒蛾重新变白的无规律性，以此质疑胡椒蛾变色未必是由于雀鸟捕食的选择，认为也许有其他因素发生作用。<br />
第八章――达尔文的地雀<br />
一个质疑是那十三个地雀种类其实没那么多，并且达尔文当时并未受到这些地雀的启发，他完全忽略了这些地雀的区别。其次是虽然有记录某个岛80年代中期曾因几年的连续干旱导致地雀的啄变大，但随气候恢复正常，它们的啄也在几年间复原（注意：跟变大的速度一样！难道气候好了会使啄大的鸟显得应该被淘汰？不过达尔文之流还有性选择保驾护航，他们总是能够自圆其说）。本章认为自然选择的确有，但一点都不神奇。<br />
第九章――四翼果蝇<br />
主要说这玩意并不是一般意义上的基因突变的产物，而是三种已经发生变异的怪物反复杂交之后诞生的，而且必须技巧非常高超才行。并且这些怪物实际上没有任何生存能力，是进化的死胡同。所以，这只是一个单纯的实验，跟进化论没有任何关系。<br />
第十章――化石马和定向进化<br />
主要是针对新达尔文主义提出的进化无定向进行批驳。作者在此倾向于“定向创生”。这章我没怎么细看。其实古尔德也研究过这个玩意，认为马的进化是一个失败的典型，由几千个品种淘汰到只剩几种。并且他认为人类也很危险。<br />
第十一章――从猿到人：终极的圣像<br />
主要是化石数量稀少，化石拼接随意性大等等老生常谈。估计作者对这个行业涉足不深，只是不能不写这一章，只好匆匆一笔带过。<br />
第十二章――科学还是神话<br />
对达尔文主义者垄断教育系统的血泪控诉，并挑拨不明真相的群众找自己选区的议员收拾那帮贱人。<br />
附录就是本书作者对各界批评的回应。自说自话没意思，回应批评可以看出作者对具体批评内容的挑选，从而得出此人到底是在瞎掰还是胸有成竹。而我对这一篇的最满意的――作者很勇敢，比方舟子在21世纪还拿19世纪甚至中世纪的傻逼开涮强了不止一百倍。并且我感觉虽然作者不可能认识方舟子，但这篇回应好像每一段都是在扇方舟子的耳光，老实说我很爽。<br />
知识点方面，米勒实验、同源肢体、海克尔的胚胎、地雀和果蝇几章是精华。生命树一章说得太笼统，不如找古尔德《奇妙的生命》来看收获大，此书直接就把生命树给放倒了。并且还能大致上了解一下古生物学家是怎么玩化石的。<br />
顺便介绍一下，古尔德是个非常自负的坚定的进化论者，且以达尔文主义正统传人自居，对以道金斯为代表的新达尔文主义是刀刀见血，毫不留情。难怪很多反进化论者都拿古尔德的成果向进化论开炮。其实这个二逼提出的“有希望的怪物”理论，逻辑上根本行不通，比新达尔文主义逻辑完美而证据阙如好对付多了。</div>

							
							
							
							
