													<h2>两会代表、委员的“雷人语录”</h2>
						
						
																											<div id="postmessage_509263" class="t_msgfont">中新网3月8日电(邓永胜) 近年来，层出不穷的“雷人提案”在全国及地方两会上已不鲜见。随着2010年全国两会大幕的徐徐拉开，新一轮“雷言雷语”陆续有来，一些代表、委员的“雷人语录”经媒体披露后立刻受到社会大众的广泛关注，在网上更是招来网友“板砖”无数。 <br />
 <br />
<br />
　　雷人语录一：老婆做家务，老公应付工资<br />
<br />
　　网友回应：张晓梅，你老公叫你回去做家务<br />
<br />
　　两会伊始，全国政协委员张晓梅的一项旨在实现“女性家务劳动工资化”提案立刻在网上引起轩然大波，受到众多网民猛烈抨击，并被选为2010年两会“雷人提案”之最。<br />
<br />
　　面对多方质疑，张晓梅对媒体称此提案初衷为切实保障女性权益，强调夫妻之间应当互相帮助。至于是否会针对一些反馈意见进行修改，张晓梅说要看到意见再决定。<br />
<br />
　　当事者的解释并未消解舆论的批判之声，有评论称，张晓梅的建议实质上是在离间夫妻关系，使夫妻关系雇佣化，从而加剧家庭矛盾。更有论者语带讥讽的喊话：“张晓梅委员，你现在可以不用去开会了，别提案了，回家去，你老公叫你回去做家务。”<br />
<br />
　　雷人语录二：“扫黄”意涵不明，应改为“扫色”<br />
<br />
　　网友回应：还不如改成“扫雷”<br />
<br />
　　日前，全国政协委员、河南省政协主席王全书建议将“扫黄”改为“扫秽”或“扫色”。此提议一出立刻遭来网友拍砖，笑称不如改叫“扫雷”。<br />
<br />
　　据媒体报道称，建议修改“扫黄”一词，是因为王全书委员觉得，黄色是尊贵的颜色，不能用于指代色情行业。　王全书说，在中国公众话语中，应当摒弃用黄色特指色情、淫秽，建议把“扫黄”改为“扫秽”或“扫色”。既可增强打击的针对性、准确性，又还原了黄色的本来意义，为“黄色”正名。<br />
<br />
　　对此番言论，网友犀利反问道：黄色尊贵不能扫，那什么颜色不够尊贵可以任意践踏呢？ 此外，更有学者表示，“扫黄”为约定俗成的说法，修改实无必要。<br />
<br />
　　雷人语录三：医改如果成功，是全国人民的悲剧<br />
<br />
　　网友回应：医改成功是既得利益者的悲剧<br />
<br />
　　“医改如果成功了，是全国人民的悲剧！”“医药分家的医改简直太荒唐了！”全国政协委员、山东工商联副主席、青州尧王制药有限公司总经理宗立成抵京之后连连爆出惊人之语。<br />
<br />
　　宗立成认为，医改的核心“医药分家”从根本上就是荒唐的，不切实际。“我自己从事药品生产，太清楚这里面的关系了。”宗立成说，“医药分家之后，等于给药品流通增加了中间环节，企业回龙资金更没保障，这不利于医药产业发展。”<br />
<br />
　　对此番言论，有人指出，宗立成自己是药商，当然站在自己的立场上讲话；也有网友一针见血的评论说，医改成功是既得利益者的悲剧，就不该让这些医药企业的人参与讨论。<br />
<br />
　　宗立成则回应称：“我个人什么都有了，犯不着为自己说什么。我是全国政协委员，到北京来就是为全国人民说真话的！” <br />
<br />
　　雷人语录四：买不起房，不应埋怨政府和开发商<br />
<br />
　　网友回应：老百姓的抱怨天经地义<br />
<br />
　　全国政协委员、地产500强百步亭集团董事局主席茅永红日前称，老百姓本身没有买房能力，就不要来埋怨政府、社会和开发商。他同时认为，房价不能打压，而且也打不下去。<br />
<br />
　　比起其他的“雷语”，茅永红的言论可算是老话重提，聊无新意。近年来，房价的节节飙升让越来越多尝够“蜗居”之苦的老百姓沦为“房奴”。而每每全国两会召开之际，总会有一些代表委员无惧民意，站出来为房产商摇旗呐喊，虽招来网民怒骂仍在所不辞。<br />
<br />
　　对此，有媒体评论称，老百姓抱怨房价高，有自己充分的理由，而且是痛入心扉的理由。房地产已经成了吸金的黑洞，全国各地老百姓的血汗钱源源不断地被它吸进去，却总是填不满。商品的价格不能长期违背商品的价值，这是经济学的最基本规律，然而，我们的房价公然地背离价值、背离居民收入，而且是全国性的、长期性的、大幅度的背离。遭遇这样病态的暴利产业，老百姓的抱怨天经地义。<br />
<br />
　　雷人语录五：取缔社会网吧，由政府办公共网吧<br />
<br />
　　网友回应：过于极端，因噎废食之举<br />
<br />
　　全国政协委员、重庆陶然居饮食文化(集团)有限公司总裁严琦表示，将在今年全国两会期间，提交关于关闭社会网吧的提案。严琦称，目前网吧已成社会顽疾，“针对顽疾，就应该下猛药”。<br />
<br />
　　严琦的说法引来众网友PK，支持者认为这个提案对社会安定、青少年身心健康都是有益的，未成年人如果到网吧上网，只能逃学或者夜不归宿。而反对者则认为这一提案过于极端，实属因噎废食，网吧由国家接管更是弊大于利。据各大门户网站调查，有六成网友对这一提案持反对态度。由严琦担任董事长的陶然居餐饮集团网站更是一度被黑。<br />
<br />
　　随后，在全国政协委员分组讨论现场，严琦又对此前言论进行了澄清，表示“关闭社会网吧”说法实属媒体误读，其原意是政府要关闭那些没有牌照的、不健康的非法网吧，然后逐步让政府来主导。对于此项提案，全国政协委员、文化部文化市场司副司长张新建作出了回应。他认为，网吧接纳未成年人需要发挥政府部门和企业两种管理作用，而高科技手段将是网吧管理趋势。<br />
<br />
　　雷人语录六：冬奥夺冠，应先谢国家再谢爹妈<br />
<br />
　　网友回应：于副局，你out了<br />
<br />
　　“感谢你爹你妈没问题，首先还是要感谢国家。”昨日上午，国家体育总局副局长、国际奥委会副主席于再清参加全国政协体育界别分组讨论，谈起冬奥会女子短道速滑1500米金牌获得者周洋夺冠后感谢父母，于再清说：“小孩儿有些心里话没有表述出来”，运动员得奖感言“说孝敬父母感谢父母都对，心里面也要有国家，要把国家放在前面，别光说父母就完了，这个要把它提出来。”他表示，要加强对运动员的德育。 <br />
<br />
