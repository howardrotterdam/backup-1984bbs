													<h2>一个公务员工作七年后的感言</h2>
						
						
																											<div id="postmessage_504876" class="t_msgfont">一、大学快毕业的日子<br />
我是学会计的，当年也算愤青，在学校里一边混日子一边想着以后毕业找个企业做会计，最好是外企，或者进个事务所都不错，所以我一直也没申请入party，后来才知道后悔，这是后话不提。我2000年拿着52份简历跑到深圳，我不是名校，成绩也不好，只是没补考而已，在学校也就不过当个系宣传部副部长。我磨破嘴皮投出去26份，最后只有3个单位找我面试，记忆最清楚的是一家公司要招3个财务管理职位，我上午进面试会议室发现居然有将近50人在等待，而且这还是第一批，下午还有第二批，结果也不说了，没戏。但是没想到的是我当年10月份稀里糊涂报考的公务员居然笔试过关，我抱着一线希望跑去面试，面试后居然又稀里糊涂过关。<br />
就这样我在大四下学期看着同学们焦头烂额找工作，而我每天通宵打星际和CS，然后毕业去单位报到。其实最开始我完全没想过当公务员，因为我觉得那种工作太沉闷而且太受约束而且我是愤青，我那时候想当然认为公务员都是吃老百姓的饭不为老百姓干活的，而我当时又确实找不到工作，现在有份收入不错的(自以为的)薪水，又很轻松的(还是自以为的)工作我为啥不去呢，就这样，我稀里糊涂当了公务员。<br />
二、你们最关心的收入<br />
我一开始上班后是单位安排的宿舍，6人一间，什么都没有，我想着咋这么差的条件捏。发工资，不错，我01年年薪居然有近5W大洋，而我的同学们进内地比较好的单位的收入也才只有3W不到，我一下成为我们班收入最高的人<br />
这里说一下题外话，讨论和比较公务员的收入不要算月薪，只算年薪，全部按2001年时间比较――我初进单位每个月拿3300，我下铺哥们进安徽某银行一个月拿1200，但是我全年所有收入才5W不到，摊下来月均4000我哥们奖金加提成居然快4W(他做贷款的)，我另一个同学，也是一个寝室的，去了清远财政局，一年只有1W出头，而我一个高中同学在深圳做软件，他一个月起薪才2000，但一年实际拿了20W。到2008年，我们再比较了一下――我正科级，刚转的(你不服，因为我牛逼，下面慢慢说)，一年整10W，阳光工资后，月工资暴涨到了8000多，然后啥也没有，不是公务员的朋友别出声，我看贴无数，没人信的，你说我年底双薪，双薪居然才749块基本工资，国家政策么，做公务员的就是服从，多发牢骚也没用。说实话，阳光工资对深圳没啥影响，貌似还小加一点，广州的就崩溃了，砍掉一半，你们不信广州公务员4000块一个月然后啥也没有的话，我也无所谓，这就是事实。至于我那个银行的同学，现在一年 15W，我那个做软件的同学，已经不做软件了，因为他头发白了一半，累得，05年拿着几十W跑去杭州炒股，赚了赔了咱不提了，你自己猜。那个在清远财政局的同学，可怜，一年摊下来每月还是才2000出头。<br />
题外话有点多，转回正题。反正我进单位后，没觉得公务员有啥好，累得要死，你们在企业的别和我说加班，老子加班的数量已经无法计算，我咋也没想到，我跑到一个go-vern-ment机关做个破会计咋会这么累，我当时毕业前跟在一个会计师事务所从2月-5月做了3月审计，虽然也是累得够呛，每天低头太多脖子巨痛，但是还是感觉没当公务员后累，估计那时肾好。不过加班多也有好处，考核连续优秀多啊，我1年转正，3年转副科，3年转正科，也算是坐火箭了(运气也很重要，我们科没年轻人和我争啊)，我一个月8000多是正科水平，要是你混的不好，还是科员，一个月起码少我1000多。<br />
<br />
三、心路历程<br />
又跑题了，这么说吧，我刚进单位前4年，完全是把这份工作当做一份养家糊口的工作，我是愤青，我严格要求自己，一定不能做我当初想象中的公务员，我做事情要尽心，要有责任心，要有职业道德，我真的很努力的去做事，既然入了这一行，就要做好。而且，就我看到那些99年，00年进单位的大哥大姐一个个都是很用心的去工作。绝对比坛子里一些所谓的在企业做看不起公务员的人强。<br />
到了05年，我心态发生急剧变化，我想当官了。。。。。。刺激我想当官的原因是和我年龄差不多或者比我小的**开始当我领导，那种感觉真的不好受<br />
以下很重要，我的个人独白：<br />
任何一个人做任何一份工作都要有当官的想法，所谓的官不仅指公务员的官，而是泛指任何单位中处于领导地位的人。我老头在90年代初来深圳打工，那时候一年 5W，10年后他一年才12W，还算了股份分红，而和他一起来的一个人当了部长，现在一年24W，另一个当了副总，一年60W，企业里面也要当官啊，一个副老总出去也是一群人前呼后拥，和go-vern-ment机关不一样么。我以前年轻，老是想着我要努力做好本职工作，不做贪官污吏，而且做官以后不干具体业务，浪费我的专业技能，现在看起来真TM扯淡，幼稚，太幼稚！！<br />
当官有啥好，一是钱多二是地位高，别管是公务员还是企业，都一样，我那些进四大的同学，现在也才一年10W，也是累得像狗样，那个去银行的现在调到他们行总部办公室做个小头头，论级别也就算副科长吧，一年将近20W，还没算奖金。你说你一个人在哪混不都是图个出人头地，图个工作轻松地位高钱还多，要得到这一切，就是两个字――当官。<br />
<br />
四、怎么才能当官<br />
有了目标，俺就要努力啊，可是家里也没教过，自己以前也很清高，以为自己业务能力强，单位离不开我，也没给领导拍过马屁送过礼，不过没关系，咱学还不行么<br />
但是，我发现，前面4年咱没玩过这些手段，现在猛一用，还不是那么回事，为啥呢，我喝口咖啡先。以前咱不大交际人际关系，也不溜须拍马，更不送礼，现在你没事说请大家吃饭，别人看你眼神怪怪的，跟大领导送礼，别人根本不要，为啥，以为你有事求他呢，请大领导吃饭，没天大的借口谁和你吃，级别根本不对应，理都不理你，所以捏，我只好转回正道，依旧努力工作，这玩意其实大家都看在眼里，虽说我现在还是平头百姓，起码转了正科，保不准明年升我一官半职，咱不说别的，起码以后是我安排别人干活不用自己具体操办了，至少工作没那么累了。<br />
据我观察，至少在我单位，大部分提拔的年轻人都还是有真才实料的，都是努力工作型，都是办事牢靠型，没有说靠拍马屁升官的，至于你要说别人送大礼包，那我就不知道了。反正我们大领导说了一句很耐人寻味的话：你要我提拔你，那你想想你这几年给了我什么帮助。我算了算，我啥也没，我一不能写材料写总结写讲话稿，二不会搞接待组织会议，三不能在酒桌上放倒别人，也不能帮领导挡酒。。。你说说你当个会计当的再好有啥用？你就是做帐一分钱不错有啥用？你就是加班加点工作的再出色有啥用？国家付了我工钱滴！！！做工作再好，只是完成你的本职，和提拔你没关系，国家付了你工资，什么都不欠你，和企业一样，不满意你自己辞职。而要领导提拔你，你就要能对领导有所帮助，就是这么简单，如果你自己觉得你足够牛逼，自己竞争上岗去吧，但最后讨论的时候，还是回到这上面，考试 100分又怎么样？你能主持工作吗，你能对领导有所帮助吗，没有的话，一边打酱油去。<br />
<br />
<br />
五、对那些年轻的想考公务员和混日子的公务员还有辞职的公务员想说的话<br />
从我个人角度来看，04年-07年进单位的那些公务员和我们99年-03的公务员比，年轻人的素质是越来越差，我不怕你们80后的说，这就是事实，几个致命缺点：最要命的是不把你说话当回事办事还毛糙不牢靠，其次是生活懒惰水平有限想法还多，第三是心浮气躁不够踏实自视过高<br />
go-vern-ment机关就那么回事，有干活累死的，有喝茶看报的，这是岗位设置问题。领导也都很忙，忙啥？我知道你们都说，当官的无非就是写“同意”两字、开会、吃喝而已，其实不然。领导挺累的，为啥呢，他要统管全局，要负责任，大大小小鸡毛蒜皮的事情全部要管，还要心里有数，万一下面出个漏子，有人骗他咋办，我们做具体事情的是身体累，我头发都白了好多，而领导心累，他要不停的想问题，为啥总是开会呢，集思广益，权衡利弊，分析细节，平衡关系，主要要想到你们没想到的东西，平衡和协调各方面利益与关系。前段时间领导给锻炼机会让我筹办一个会议，我才知道原来开个会有这么复杂，而且好多我没想到的细节问题，都是领导指出的，开个会，你要做好名单制定，通知发放，人员接送，住宿，吃饭，会议场地布置，领导发言稿写作，参观路线，导游。你们别说什么公款吃请公费旅游，很多大型涉及到吃请旅游的会议，都是上面来人才搞，自己人开会直接在个会议室不就完事了。俺把这次会议办的周到体面还省钱，上面对他满意，他脸上有面子，这才算帮到领导一次，我容易么我。<br />
老是跑题，乱写一气，你胡乱看，说回前面的，我带过好几个年轻人，基本04年后的办事都不牢靠，一件事情，你交代他几次，有时还会错，还有极品就是你说几次让他某时间前把一项工作做完，他老人家随意敷衍我，到最后居然根本没做，我当时肺都快气炸了。还有的人看见大家忙的很，工作做不完，他说晚上有事，就是不愿加班，我知道没加班费，可我自己这么些年加无数班领过一分钱加班费吗？我还不是卖力做事(解释：公务员、军人、农民、保姆均不属劳动法法律对象，没有加班工资一说)。反而老**经常加班，虽然她们不大懂电脑，但她们就是安心做打字员，你教她们做她们就按你要求做，而且很认真很仔细，我不敢说老**业务水平高多少，起码别人工作态度在那，责任心比你们强了去了。有好几次，别的单位的老会计来我们这看见我们太忙就坐下来帮我们，叠单据打文件拿资料什么，小年轻们看见我们忙反倒跑的飞快。<br />
做了7年公务员，我原来单纯把干公务员作为做养家糊口的工作，现在逐步过渡到把这份工作作为一份事业来做，说实话，我中级会计证有，也在读在职研究生(注会考不上啊考不上，现在有小孩也没空搞)，不干公务员我去企业适应几个月做企业会计，在深圳随便找个月薪 6000以上的工作也没问题，7年工作经验不是哪个大学生谁都有的，不过说真的，在企业还是好，我现在的同学好多年薪都比我高，有的好企业一个月只发 4000块，但是年底奖金4-5W，还有季度奖，如果有进中层管理的，年薪就翻倍了(我只知道1个。。。)。但是我现在的想法是，人往高处走，如果我要辞职，除非找到收入更好发展更好的，或者go-vern-ment减薪。这个社会有时薪水不如人际关系重要，很多人为啥永远干公务员，他已经没有什么外面的人际关系了，他要辞职找工作，找谁帮忙，而且公务员没社保，40岁辞职，养老咋办。<br />
<br />
 六、对那些对公务员不满的人说的话<br />
有些人在企业总觉得自己很牛逼，什么这个论证那个分析什么提供决策，都扯淡，说白了就是开会，什么加班加点到凌晨，我们一样加班，说实话，我看我们单位那批99年的，无论去哪，都会出人头地。你要搞科研就不要和公务员比，没可比性，其实我还挺羡慕做技术的，你们搞出个东西那是成果，起码你自己有成就感，我们呢，估计pol.ice破案有成就感，大多数人真的工作N年啥成就感也没有，就是完成本职工作。<br />
很多公务员挺可怜的，30好几还是个副主任科员，累得半死，领导天天强调服务服务，那些坐对外窗口公务员的说白了就一坐台的，你说公路收费站的小妹、马路上吃灰的交警、国土局收文的窗口、税务局报税的，天天看无数的人还要微笑服务，脸都笑僵的。我做会计，我老婆说我进单位前几年每天脸上洋溢的微笑，现在每天都是猪腰子脸，我累啊，我笑的出来么，我又不是卖笑的。内地的公务员还好点，起码城市人口少，深圳300多万常住人口，1100流动人口，一共是1400万，北京是1630万，大城市的公务员每天面对这么多人，笑的出来才有鬼。不是老有人骂银行么，说他们动作慢，排队长，服务差，都一个鸟样，中国什么都不多就是TMD人太多。<br />
每年都有辞职的，每年也有削尖脑袋想进来的，无非就是想找份工作，现在工作难找啊，企业效益不好，就没钱发，这点公务员倒是挺有保障，算个亮点吧，可是我是做会计的，从02年起，国家财政就没给公务员加过钱，用俺们术语叫做――人员经费零增长，NND报纸上老是登什么中央计划给公务员加工资，实际呢，加了个P啊。想辞职的就辞职吧，什么看不惯官僚作风，看不惯领导做派，看不惯这看不惯那，等你到了企业你会发现，嘿嘿，都一个鸟样，你不要以为企业就不会管理你，打卡上班比公务员还严，你不要以为到企业就能实现凭本事怎么这么样，真的，都一个鸟样，我在企业的同学以前一个个羡慕的我不行，现在他们混出头了，都是骨干了，也没什么人说会羡慕我了，你说混的比较差的，他会羡慕所有比他好的。<br />
天亮了，今晚通宵，我真强，马上又要开工咯，周五了，明天睡到自然醒，估计又要被老婆骂死。。。。。。。<br />
乱七八糟的，您也别找什么BUG，看的懂你就看看，看不懂就当笑笑，这就是真实的生活，对那些想考公务员的年轻朋友，如果你能考进来，如果你想干这行，做好思想准备，这行不好干，其实哪行都不好干，都要付出努力，我可以保证，公务员这份工绝对不是你想象中的那么美好。<br />
我希望你们，无论你干哪行一定要――改掉大学睡懒觉的习惯，改掉大学随意不上课的习惯，改掉老子第一天下无敌的习惯，任何单位任何同事都不喜欢这样的人<br />
我也希望那些年轻的刚进单位的公务员，办事情一定要注意细节，一定要稳重，没理由交办给你的事情你搞了半天给我个错误的结果，我要你干啥，要是企业早炒你了，一定不要懒惰不要迟到不要缺勤不要躲加班，一定不要看不起老**，一定不要觉得领导没水平，告诉你，没水平的人当不了领导，他能当官，就一定有某方面的过人之处，你们要去寻找并学习。我相信这几点不仅对于公务员，对任何单位都是一样的。你们进单位时一定听过一句话――年轻人，多干点没事的，是啊，我前几年累得半死，觉得这帮老家伙就是不愿干活，把什么东西都推给你，非常烦躁，但是现在的我，觉得这句话真的不错，干活多，是苦是累，但是你有经验、有人缘，这些是买不到的，都是你平时多干活点点滴滴累积起来的，真的，年轻人，多干点没事的！！<br />
工作七年，一点感触，希望这里面能有一部分与大家共勉！！！<br />
 <br />
喝酒感悟：(不特指公务员之间，可以用于所有场所)<br />
&nbsp;&nbsp;我酒量非常差非常差，从大一开始练也没练出名堂，尤其是白酒，三两必吐。但是我现在发现，其实，酒桌上有时并不需要你有太多酒量。要点：<br />
&nbsp;&nbsp;1、酒未喝到话要说到――你级别低，又喝不下，但是你一定要把该说的话说到<br />
&nbsp;&nbsp;2、喝酒切忌被动――你级别低，刚进单位怕乱辈分，总是等前面人敬完你再敬，大错特错，你酒量再差，等大领导敬完客人第一杯后，马上出击，决不能让对方有人先敬你<br />
&nbsp;&nbsp;3、想法设法让对方喝下更多的酒――记住，一个会劝酒的人比会喝酒的人更让自己人欢喜，领导最喜欢有本事灌别人的人，一般喜欢能帮他挡酒的人，不喜欢即不能劝酒又不能挡酒的人，最讨厌被人灌翻丢人显眼的人<br />
&nbsp;&nbsp;<br />
&nbsp;&nbsp;如果你不具备以上条件，那就去学习<br />
&nbsp;&nbsp;<br />
&nbsp;&nbsp;中国的酒文化啊，挺无奈的，真的，但是这就是社会，这就是现实，但是不可否认，酒一喝，气氛就来了，陌生的人马上就熟悉起来，一切事情就好办了，您别笑，企业也是一个鸟样，我老爸虽然业务熟练，但要不是能喝酒，早被老板炒了，谁还聘用那么老的员工啊<br />
<br />
&nbsp;&nbsp;<br />
&nbsp;&nbsp;还想说两句：<br />
&nbsp;&nbsp;<br />
&nbsp;&nbsp;我发现这坛子里很多辞职朋友写感受，发现一个特点，基本都是干3 年以内就觉得干不下去的，觉得好像看透一切一样，其实这是一道坎。我个人认为，人尤其是男人在20多岁工作后才慢慢成熟，基本上大学毕业参加工作以后，心态发生巨大变化，因为一切都和学校里面不一样了，你进入社会了，你接触的人的类型多了，你和其他人开始有利益冲突了，最重要是你受到各种规章制度的约束了<br />
&nbsp;&nbsp;有些人觉得工作沉闷，千篇一律的重复机械劳动，没有成就感，还没有表扬和激励；有些人觉得人际关系不好处，你不服管领导就给你穿小鞋，同事觉得你太牛逼就排挤你；有些人进入公务员队伍后发现理想与现实落差太大，收入太低福利待遇不好，尤其是总觉得自己干了很多却收获很少<br />
&nbsp;&nbsp;以上种种，皆是心魔，都是人成长道路上必经的，正所谓此处不留爷自有留爷处，你走人没问题，但是记住我一句话――如果你是带着怨念走的，我看死你，你以后一定不会有发展的；如果你是带着感激走的，我看好你，你一定有美好的明天<br />
&nbsp;&nbsp;人的心态，三年一熟，我自己也很惊讶，我01年刚工作时，满腔热血，斗志高昂，梦想靠双手打下一片天地，我当年副科提拔不上，单位另一个年轻人却提拔了，我自认他脑子不如我聪明，工作不如我娴熟，但是别人就是提拔了，我却没有，我心灰意冷，充满怨念，尽管我依然保证把工作出色完成，但是我的满腹牢骚和消极心态，领导也没给过我好脸色，我一直到现在也没提拔，就在此处。07年开始，我不再发牢骚，我不再有怨念，我依然努力工作，而且是比以前更勤奋，端正心态，更注重细节，尤其是个人生活上小细节，领导又逐渐对我态度好起来<br />
&nbsp;&nbsp;所以说，一般大学生工作4-5年后，都有一道坎，看你自己心态，和你一起进单位的，别管是不是机关还是企业，4-5年后，高下立分。<br />
&nbsp;&nbsp;辞职的朋友们，你们想想，难道和你一起进单位的人，个个都是像你这样受委屈，他们中就没有比你混的好的？你以为工作3年就真的看透一切，我7年都不敢说这话。你不得宠不代表所有人不受赏识，我希望还是多从自己身上去找缺点<br />
&nbsp;&nbsp;社会，永远是你去适应它，而不是你去改变它，什么公务员领导一言堂，什么勾心斗角，什么付出与收获不符，其实哪里都是一样，我老头在国有企业17年，工厂效益太差不得已去打工，又在私营企业做了15年，见过的东西实在太多，国有企业私人老板都是一把手拍板，这不都是一言堂？升迁与收入挂钩，而职位有限，为了利益，同事之间表面一团和气，背地互相拆台，难道没有勾心斗角？我老爸年纪大又没有高学历，企业给他定的薪水低，但是活干的不少，这付出就一定与收获相对等？不要找外部原因，多想想自己，改掉自己的缺点，学习别人的优点，这样的人才能进步<br />
&nbsp;&nbsp;工作干的不顺心，首先想想自己的心态有没有放正，自己有没有努力，自己有没有不足，如果你心态端正，勤奋努力，没有缺点，不可能没有领导不喜欢你，但是也不能沾沾自喜，坛子有些年轻人说自己写了几个材料，加了一个月班，好像就多牛逼了，这是自以为是，后来发现领导不重视他又倍感失落，这是心态不正，这些毛病说实话我都有，关键在于自己的调整。给你活干给你担子不是压你是锻炼你，好多大学生毕业找工作抱怨说你们都要有工作经验的，我不工作哪来工作经验，经验啊，经验何其重要，比你个研究生文凭牛逼多了。话说回来，现在本科生、研究生算毛啊，大家都是大学出来的，学校里怎么混出来的咱爸妈不知道难道自己还不知道，我时常自觉庆幸我是97年进大学，98年开始扩招后那个大学教育流水线的产品是一代不如一代，你说呢<br />
&nbsp;&nbsp;我们有什么，我们年轻，有体力，我们懂电脑，自学能力强，会利用软件快捷方便的办事，老**拿着小本登记计算器加减，俺们直接EXL一拖汇总，对不，我们接受能力强，上手快，一教就会<br />
&nbsp;&nbsp;我们缺什么，我们缺经验，我们缺资历，我们就是一个身怀绝技但缺乏实战经验的高手，但这种高手很可能被一个街边打过无数场群架的小混混一板砖拍倒<br />
&nbsp;&nbsp;我还是希望，年轻的朋友们，正视自己，改掉缺点，大学养成的懒散习惯会害死你的，少发牢骚多干活，一切别人都看在眼里，该是你的一定就是你的，只不过每个人的时间长短不一而已，相信自己！</div>

							
							
							
							
