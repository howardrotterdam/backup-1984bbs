													<h2>“民主的专制”与“多数的暴政”</h2>
						
						
																											<div id="postmessage_433585" class="t_msgfont">　　<br />
　　作者：邵建 文章来源：民主中国&nbsp; &nbsp;&nbsp;&nbsp;时间：1/14/2010<br />
<br />
　　<a href="http://www.minzhuzhongguo.org/Article/sf/201001/20100114084803.shtml" target="_blank">http://www.minzhuzhongguo.org/Ar ... 0100114084803.shtml</a><br />
<br />
　　前几天我曾用梁启超的概念“民主的专制”剖析新近发生的一则新闻事件，文章发出并上网后，陆续看到了些批评。大都反对这个由当年梁任公提出的概念，认为这是人为制造混乱和困惑。因为批评者坚持认为民主与专制是对立的、互不相容的，怎么会有“民主的专制”呢。<br />
<br />
　　把民主与专制对立起来，是《新青年》以降的事。如果可以把时间往前推，或者，如果我们愿意读一些《新青年》前十年的文本，亦即有关清末立宪运动的文本，可以看到，无论是和清廷对立的立宪派，还是清廷内那些赞同立宪的廷臣疆吏，无不是用立宪（或宪政）的概念以与专制对立，正如用君主的概念以与民主对立。君主与民主，或，宪政与专制，前者是国体范畴，后者是政体范畴。君主也好，民主也罢，它们都可以立宪，也都可以专制。这是那个时代的政治学ABC。然而一百年过去了，我们的认知竟不及当时，殊让人长叹息。<br />
<br />
　　当然，也有网友在跟帖中问我，“民主的专制”是否就是“多数的暴政”，答案应该是肯定的。梁启超是中国的托克维尔，他和托克维尔都去过美国，并且对美国都有独到的观察。梁启超虽然没有读过托克维尔，但他的“民主的专制”与托克维尔的“多数的暴政”却堪称异曲同工。鉴于对我的批评中有对梁任公的不信任，以下我不妨用“多数的暴政”来解释“民主的专制”。<br />
<br />
　　民主，首先是一个政治学概念，它指的是国家政治权力在人民手里，即人民主权。然而，在西方古代社会，统治权力既可以在一个人手里，也可以在少数人手里，还可以在多数人手里。以古希腊为例，城邦由一个人统治，那么，这是君主政治。如果统治者是少数人，则可称贵族政治。如果由多数人实行城邦统治，就是所谓的民主政治了。我们知道，当时古希腊的雅典、斯巴达等城邦，都是由多数人直接统治的民主政治。但，这三种政治形态，在亚里士多德那里，无所谓好坏；或者说，它们都具有两面性，可好可坏。具而言，一个人的统治，其统治要旨在于照顾整个城邦利益，这就是君主政体；反之，他只照顾统治者的一己之利，那么，该政体则沦为僭主政治。同样，少数人统治如果不是为了多数而只是为了他们少许富贵人的利益，贵族政治则沦为寡头政治。以此类推，多数人的统治如果不是为全体利益，而仅仅是以他们自己的穷人利益为依归，这就是平民政治乃至暴民政治。由此可见，三种政治形态，俱不必然为好，亦不必然为坏。<br />
<br />
　　专制也是个政治学概念，我们对专制的理解，即统治权掌握在一个人或一个对象手里。这当然是专制的一种形态，但它更重要的形态却是该权力因不受限制而变成一种无限的权威。在《美国的民主》中，托克维尔指出“无限权威是一个坏而危险的东西”。“当我看到任何一个权威被授以决定一切的权力和能力时，不管人们把这个权威称做人民还是国王，或者称做民主政府还是贵族政府，或者这个权威是在君主国行使还是在共和国行使，我都要说，这是给暴政播下了种子。”托氏的表述很清楚，这种不受任何限制的权威所导致的暴政，即可以来自君主，也可以来自民主，包括来自贵族。以上亚里士多德所论述的三种政治形态，一一出现在托克维尔的笔下，它们都可以形成暴政。这里，民主的暴政就是多数的暴政；因为民主一般做不到全体，往往表现为多数。<br />
<br />
　　在托氏那里，“多数的暴政”与“多数的专制”两者互用，后者即梁任公的“民主的专制”。托氏甚至认真比较了民主专制和君主专制的不同，指出“民主的共和制度不依靠物质力量进行专制”。如果说君主专制以迫害身体的办法压制灵魂，民主的专制或共和专制却可以放过你的身体而直接压制你的灵魂（即思想）。所以，托氏警告当时的美国人：“君主的专制政体已使专制为人们所不齿。我们可要警惕，别让民主共和国使专制死灰复燃。”<br />
<br />
　　民主为何可以专制，无论托氏还是梁氏，指的都是权力不受制约。君主与民主，都是权力范畴的概念。只要是权力，就有专制的可能，民主自不能例外。那么，如何革掉专制，梁任公认为民主是靠不住的，只能靠以立宪为开头、以法治为诉求的宪政。所以以他为代表的立宪派在清末掀起了颇为壮观的宪政运动，可惜功败垂成。切换到托克维尔那里，他所指的不受限制的无限权威，就是不受法律限制。因此，他紧接着说：只要有这种无限权威的地方，哪怕是民主的无限权威，他都要“设法离开那里，到别的法制下生活”。惟有法治，才能有效地制约权力。所以，在指出多数的暴政之后，托氏立即把论述的重点转移到法治上来。他非常推崇法学家，认为“法学家在行将诞生的民主政治社会或许负有首要的使命”，因为法学家的职业特征使得“他们特别反对革命精神和民主轻率的激情”。这样的表述，很切合梁任公他们在清末做的工作。在宪政和民主都不具备的情况下，宪政先行，而非以革命求民主。革命后的民主，因其暴力的惯性，无以给它套上宪政的笼头。<br />
<br />
　　什么是民主、什么是专制，从托克维尔到梁任公，是一个知识脉系。然而，自《新青年》之后，这个知识脉系为另一种有致命缺陷的民主理论所取代。我们今天对民主的认知，是《新青年》以来的认知，然而，这样的认知是对梁任公时代的倒退。因此，无论在知识上，还是运作上，我希望“回到梁启超”能成为我们今天诉求宪政与民主的自觉。</div>

							
							
							
							
