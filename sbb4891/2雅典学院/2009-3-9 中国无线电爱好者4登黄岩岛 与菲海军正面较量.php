													<h2>中国无线电爱好者4登黄岩岛 与菲海军正面较量</h2>
						
						
																											<div id="postmessage_48527" class="t_msgfont"><img src="http://www.chinanews.com.cn/gn/news/2009/03-09/U30P4T8D1593320F116DT20090309093839.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
15年来，一批中国无线电爱好者试图通过自己的努力，从遥远的黄岩岛发出无线电信号，维护我国主权　<br />
<br />
　　国际先驱导报见习记者方辉 记者窦晨发自北京 这好像一场游戏，但实际却承载着一定的政治意义。在与陈平接触后，这种看法得到了印证。<br />
<br />
　　作为一名无线电爱好者，陈平曾不只一次登上黄岩岛。一听到《国际先驱导报》记者谈起菲律宾最近试图通过立法将黄岩岛列入“菲律宾所属岛屿”，已经64岁的陈平难掩自己的愤怒：“这是明晃晃地抢劫，黄岩岛是中国领土不容置疑，我们在黄岩岛的无线电操作得到了世界无线电爱好者的认可就是铁证。” <br />
<br />
　　到底是怎样的经历，让他如此愤怒？<br />
<br />
　　黄岩岛，我们来了<br />
<br />
　　黄岩岛曾名民主礁，是我国中沙群岛中唯一露出水面的岛礁。该岛距广州约600海里，距菲律宾苏比克湾120多海里，战略位置十分重要。陈平回忆说，“第一次去黄岩岛是在1994年6月，由于台风的影响，我们的船颠簸得很厉害，大家不停地呕吐，但身体的痛苦难掩内心的兴奋，当看到黄岩岛的礁石时，船上的中国人都不禁欢呼起来，有的人还在身上披上了国旗。”<br />
<br />
　　在登上黄岩岛的中国无线电爱好者眼中，那里是一个由珊瑚礁构成的几乎成等腰三角形的“环礁”，总面积超过150平方公里，最长跨度16公里。环外是3000多米的深海，环内平均水深不足10米，形态各异的珊瑚和色彩斑澜的小鱼在环内栖息繁衍，星星点点露出海面的礁石如珍珠般撒落在漫长的环线上。<br />
<br />
　　在时隔近15年之后描述当时的情景，陈平依然显得很激动。“我们到达的时候正是早晨，蔚蓝的大海在晨光的照洒下泛出闪闪的白光，那些露在水面上礁石像一个个小矮人在迎接我们的到来，比我之前看到的照片要美多了。”<br />
<br />
　　BS7H：来之不易的“身份证”<br />
<br />
　　1935年，美国人设计了一种旨在推动业余无线电活动的“游戏”，叫做“DX Century Club”。按照规则，全世界被分成许多“DXCC分区(DXCC　country)”，一个真正的政治概念的国家是一个“DXCC分区”，该国家拥有主权的并与之相隔225海里以上的岛屿也可算一个“DXCC分区”。<br />
<br />
　　这项活动很快在世界范围内的无线电爱好者之间普及，其中一些人认为，中国的黄岩岛的地理位置符合“DXCC分区”的规定，但想要证明这一点，必须到黄岩岛上架设业余无线电台，与全世界的其他“DXCC分区”的业余爱好者取得无线电联络。<br />
<br />
　　于是，一些外国业余无线电爱好者开始向中国申请登岛，由于外国人不能在中国独立设置业余电台，因此需要中国无线电爱好者加入合作。经协商，由有关部门批准，1994年，中国无线电运动协会联合美国、日本等多国无线电爱好者组织了第一次“黄岩岛远征”。也是那一次，陈平第一次登上了黄岩岛。<br />
<br />
　　15年过去了，中国无线电爱好者已前后四次登上黄岩岛(分别是1994年、1995年、1997年和2007年)，陈平自己就参加了其中的三次。如今，黄岩岛已经成为世界上三百多个“DXCC分区”之一，国际业余无线电界基于历史事实，一直坚持承认我国对黄岩岛的主权和所指配的呼号。<br />
<br />
　　“也正为这个原因，支持和配合国际业余无线电远征队赴黄岩岛开展业余电台活动，对在维护我国主权、促进南海地区的和平与稳定具有积极的意义。”陈平说，回忆起黄岩岛获得“承认”的过程，其中不乏曲折。<br />
<br />
　　第一次去时，远征队员对黄岩岛的真实状况“始料不及”。“黄岩岛很小，”陈平记得当时见过最大的礁石也只有5平方米左右，为了安全起见，队员们在搭平台的时候将4个支架插在了海水里，没想到这个保守的做法让这次远征的成果没有得到国际承认，认为黄岩岛成为“DXCC分区”的证明还不充分。<br />
<br />
　　为了让黄岩岛获得“DXCC分区”地位，联合远征队于1995年4月又一次前往黄岩岛。鉴于上次的教训，这次队员们将每个平台都搭建在礁石上。但好事多磨，这次远征队带的设备出了故障，致使电波传送的范围受到了限制，美国东部的无线电爱好者未能与他们取得联络，这带来了新的麻烦。随后在美国进行的投票否决了将黄岩岛列为新的“DXCC分区”的申请，反对票主要来自美国东部。<br />
<br />
　　为此，时任中国无线电运动协会秘书长的陈平专门赶赴美国参加辩论。他在两次会议上即席演讲阐述中国无线电爱好者的观点，得到了许多与会者的支持。1996年1月23日，美国业余无线电协会理事会终于通过决议，黄岩岛被宣布列为新的“DXCC分区”。<br />
<br />
　　如今在“DXCC分区表”中，黄岩岛以“BS7H”表示。“B”表示中国电台，而“S”表示南海诸岛，“7”表示其行政归属海南省在我国第7区，“H”表示黄岩岛。之后，为了满足世界各地无线电运动爱好者的需求，同时也为了向那些反对黄岩岛成为“DXCC分区”的人证明其价值，远征队没有停止远征黄岩岛的活动。<br />
<br />
　　五星红旗高高飘扬<br />
<br />
　　黄岩岛之旅并非一直风平浪静，特别是在1997年。<br />
<br />
　　当时参与第三次登岛行动的江苏省无线电运动协会秘书长陈方(陈平的弟弟)记得，那年4月30日，中国业余无线电远征队(简称远征队)的第一个无线电操作平台还没有搭好，就遭到了菲律宾海、空军的干扰。<br />
<br />
　　“这里是菲律宾的专属经济区，你们不能在这里执行任务。”一架标有HAF字样的F5战斗机和一架黄色的815号侦察机对着环礁外搭载无线电爱好者的中国船只俯冲而去，然后这两架飞机又分别向同行的另外一艘中国船只以及正在设置平台的3个岛礁作俯冲飞行。距离如此之近，机舱里的飞行员清晰可见，气流几乎掀掉了远征队员们头上的草帽。<br />
<br />
　　远征队员们并没有被这突如其来的变故唬住，他们依然在选定的3个岛礁上紧锣密鼓地搭设操作平台。下午，1号和2号礁上的电台便开始了呼叫。<br />
<br />
　　第二天是五一劳动节，早晨，队员醒来后就发现菲律宾的军舰依然在周围监视着，由于事态不明，队员们推迟了到岛礁上操作的时间，一直到将近10点才发出了第一次呼叫。<br />
<br />
　　这时两艘来自菲律宾海军的橡皮艇靠近了远征队员工作的礁石，“虽然他们脱下了军装，但手里握有武器，而且远处的菲律宾军舰虎视眈眈，天上的菲律宾战斗机不断俯冲。”陈方回忆道，“我们坚称黄岩岛是中国的领土，我们是在自己的领土上搞无线电试验，如果他们要上来，我们肯定要和他们斗争。”<br />
<br />
　　在远征队员的严正声明后，菲律宾海军并不死心，一艘橡皮艇始终围着架设了无线电平台的礁石转悠，想伺机登岛。但眼看“偷袭”无望，一个士兵谎称橡皮艇的发动机出现故障，需要靠在礁石上修理，还是被中国人坚决拒绝。菲律宾海军自讨没趣后，将橡皮艇停靠在一块离远征队员不远处只有板凳大小的礁石旁，并将菲律宾国旗插在橡皮艇和礁石的中间。<br />
<br />
　　见此情景，中国队员非常愤怒，一位队员立即将中华人民共和国国旗插到了无线电平台的顶端。“那就是在挑衅，黄岩岛是我们的领土，看到五星红旗高高飘扬，我们心里别提有多激动。”陈方告诉《国际先驱导报》：“我们不能忍受他们对中国主权的挑衅，这是原则问题，当时几位年轻的队员已经作好准备，如果菲律宾人敢将国旗插到礁石上，他们就要过去拔掉。”<br />
<br />
　　由于突发情况不断，远征队决定提前返回。队员们意识到要在3天的时间里完成原定7天的任务是不现实的，但为了尽量多地和外界联系，于是他们开始不分昼夜地在岛礁上操作。为了提高效率，有的队员还使出了“分区呼叫”的绝活，一分钟内可以联络5个台。到5月3日远征队结束活动返航时，共联络到6大洲40多个国家和地区14000多个电台。<br />
<br />
　　临走时，中国队员在黄岩岛畅游了一番，以表达对祖国领土的亲近。<br />
<br />
　　“支持年轻人去黄岩岛”<br />
<br />
　　1997年后，不断有中国渔船在黄岩岛海域遭到菲律宾海军驱逐，甚至有渔民被抓的消息传出。出于政治的考虑，此后10年间，黄岩岛的无线电活动一直处于停顿状态，成了世界上业余电台活动稀缺的“DXCC分区”之一，由此，国际上也出现了要将黄岩岛从“DXCC分区列表”中删除的声音。<br />
<br />
　　鉴于这种情况，在有关方面的支持下，中国业余无线电运动协会决定再次登岛，用民间活动方式申张主权。有了10年前的冲突，这次远征有了不可避免的政治意义。<br />
<br />
　　2007年4月，BS7H的呼号再次呼叫全球的业余电台，中国业余无线电爱好者的身影又出现在了遥远的黄岩岛。这次，远征队租用的小渔船从香港出发，历时3天到达，是4次中最为艰苦的一次。这次远征无疑是成功的，他们与世界45000多个业余电台进行了联络。<br />
<br />
　　“这就相当于全世界300多万业余爱好者都承认了黄岩岛主权属于中国的事实，因为我们使用的是中国的呼号，手持着中国外交部的批准文件。”陈平认为，这样能得到国际承认的方式要比挥舞国旗更具实际意义。<br />
<br />
　　面对现在的情况，陈平对今后黄岩岛的情况不无担忧，“现在菲律宾已经公开谋求黄岩岛的主权，这种情况下，我们的无线电爱好者可能很难顺利地在黄岩岛操作无线电。”他说，“我已经64岁了，今后去黄岩岛的可能性不大，但我会全力支持年轻人去黄岩岛。”<br />
<br />
