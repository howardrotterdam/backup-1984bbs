													<h2>比政府更差的是人民</h2>
						
						
																											<div id="postmessage_692017" class="t_msgfont">时下骂政府已经成为一种时尚，政府在网上已经成了一种弱势群体（个体）。我这人一向同情弱者，所以干脆不骂政府了。<br />
<br />
&nbsp; &nbsp; 自古以来，普天之下，从来没有一个国家的人民对杀害自己民族幼儿的行为拍手称快的，从来没有发生这样的事情后不是义正辞严地遣责暴徒的，而我们的人民不是这样，他们拍手称快，他们认为这是政府造成的，我知道中国人爱看热闹，没见过这么样无耻地看热闹的。<br />
<br />
&nbsp; &nbsp; 只看现实，你是绝对理解不了的，回溯历史，你就能理解，这是这个民族的本来面目。<br />
<br />
&nbsp; &nbsp; 1989年，人民和学生一起上街，他们起初以为事情能成，后来事情不成，人民快速躲起来，人民起来检举人民，一夜之间天下太平，似乎什么也没发生。<br />
<br />
&nbsp; &nbsp; 1978年，嗡嗡嗡刚刚结束不久，开始拨乱反正。人民突然将所有的错误归给他们伟大的领袖毛主席，似乎过去的10年和他们无关。似乎那些红卫兵是从外星来的，似乎那些打砸抢是地下上来的鬼干的。<br />
<br />
&nbsp; &nbsp; 在国家受到侵略时，从来没有象这个国家的人民出这么多汉奸。不管是按绝对数量还是相对比例。<br />
<br />
&nbsp; &nbsp; 在这样的人民心中，那些在别的民族被尊为民族英雄的为他们开疆掠土的伟大人物――秦皇汉武、唐宗宋祖，都是暴君，都是反面人物的典型。<br />
<br />
&nbsp; &nbsp; 这个民族有5000年的历史，却没有贵族。<br />
<br />
&nbsp; &nbsp; 现在，这个国家的人民在干什么呢？<br />
<br />
&nbsp; &nbsp; 占人口多数的农民，他们使用世界上毒性最大数量最多的农药，在为城里人（也是他们的同胞）种植蔬菜和水果。用避孕药养殖鱼虾和螃蟹。这与政府无关，这个国家的政府早就不收农民一分钱，还给他们大量的补贴，还免费为他们修路、通电视、接宽带、建社保、搞自来水。<br />
<br />
&nbsp; &nbsp; 这群人民最爱的是吃喝嫖赌，吃果子狸吃出了非典，吃鱼翅吃得全世界的鲨鱼快绝种，刚吃饱饭没几年就吃出了世界上最大规模的糖尿病。只有他们的上市公司市值中含有这么多酒精，烈性白酒成了他们价值投资的典范，还不知羞耻地自称巴菲特。<br />
<br />
&nbsp; &nbsp; 这个民族的少女中，很多稍有姿色的，都在从事世界上最大规模的色情服务，与此相对应的是，这群人民中间，有世界上最大规模的嫖客。政府是禁止这些的。<br />
<br />
&nbsp; &nbsp; 这群人民中只要有点钱的，多数都是赌徒，交易软件是他们的道具。他们中的大多数总是在骂政府，而实际上是，政府总理在1664明确地告诉他们，总理出国期间每天早上起来都要看盘，信心比黄金更重要。政府在07年下半年一直在警示风险，可这些人民不听。后来被套了，成为骂政府中最响亮的一群，他们让政府救市，政府真救市时，他们不自救，他们割肉自杀。这么愚蠢贪婪的人民，政府有什么办法？<br />
<br />
&nbsp; &nbsp; 这群人民中有些阔的买了大房子的，年年在挖在刨，小区的花园永远象工地。这群人民终于买上了车的，横冲直撞，不知道那些步行的骑自行车的也是人民。这群人民自己刚刚吃饱饭没几年，就学美国总统布什养狗，可他们不知道牵着，他们的狗咬了另外的人民或者人民的孩子，他们在一旁哈哈大笑。政府是禁止这么干的。<br />
<br />
&nbsp; &nbsp; 这群人民已经没有人再关心思想和灵魂，他们只知道不择手段地赚钱。30年以来，作家们除了一个已经死去的叫路遥的以外，基本上都在写阴道和鸡巴，蛊惑人心，诲淫诲盗。这个时代的人民没有写出一句象样的诗，人民也根本不读诗。这个时代的夜晚只有欲望，没有星空。这能怪政府吗，中国政府是世界上少有的出钱养作家的。养他们有什么用？通货膨胀来了，还不如养猪。<br />
<br />
&nbsp; &nbsp; 这群人民中间没有合格的批评者，他们只是煽动仇恨，以政府和精英为假想敌，树立自己为人民代言的形象，然后搞演讲、搞画展、搞代言，赚人民的钱，他们比纯粹的商人更可耻，商人至少是真实的。<br />
<br />
&nbsp; &nbsp; 这群人民，认为所有的官员都是贪官，所有的美女都是婊子，所有的创业者都是奸商，所有的富人都该千刀万剐，所有的中国的都是二流，所有的美国的都是普世价值。他们忘了自己5000年的历史上曾经有孔子老子，曾经有李白苏轼，曾经有霍去病岳飞，曾经有孙中山太祖。<br />
<br />
&nbsp; &nbsp; 这群人民没有信仰，从来不知道对自己的灵魂和来生负责，伟大的唐僧在他们的电视剧里成了一个搞笑的小丑，他们大多不知道，唐僧当年在印度，不仅翻译了大量的佛教典籍，也以其原创的经典被佛教的母国奉为宗教领袖，国王和万民都臣服，国王挽留他留下来担任宗教领袖，唐僧因为爱国才回来的。这群人民现在唯一与宗教有关的活动就是去烧香求财，当然庙宇在这群人民中间也成了商铺。这群人民干尽所有的坏事，他们的灵魂不愿意承担罪责和惩罚，于是轻飘地说，这都是政府惹的，谁让我是人民？<br />
<br />
&nbsp; &nbsp; 突然想起美国林肯总统有句名言：&quot;有什么样的人民，就产生什么样的政府。&quot;让这样的人民实行民主，国家将比苏联更快地分崩离析，他们将选出比阿扁更贪的总统，将会有比巴基斯坦更多的政变、比菲律宾更多的谋杀、比泰国更多的流血、比伊拉克更快地出卖自己的经济主权、比抗日战争时期出现更多的汉奸、比东欧更快地崩溃萧条。<br />
<br />
&nbsp; &nbsp; 还有这群人民无比崇拜的美国另外一位总统肯尼迪讲的：“不要问国家为你做了什么，要问自己为国家做了什么”。这样的话，这群人民是不知道的，或者知道也装不知道。他们无比崇拜美国，可不知道崇拜美国什么！<br />
