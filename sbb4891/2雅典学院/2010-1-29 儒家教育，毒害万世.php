我们为什么要从小接受教育？教育是一种影响，一种积极的影响，一种对人类认识和改造客观世界及自身的积极的影响。我们接受教育，是为了让我们具有认识与发现真理，不断进步和发展的能力。但是，我们中国人从古到今接受的教育，把我们教成了什么呢？中国人的教育，把我们教育的越来越无能，越来越“空谈性理”，不知真理，让我们变成只知撒谎，只认谎言，一代比一代落后，愚蠢的思想奴隶。从殷商时期的淳朴到唐宋时期的厚黑，再到明清时期的愚顽！<br />
庆幸的是，经过五四运动的洗礼，那个将中国人教育得越来越愚昧，越来越黑暗的长期把持中国教育界邪恶力量，受到了强有力的阻截。然而，它并没有从我们的身边消失，只不过是转入了地下，这股已经深深扎根于中国人民族性的邪恶力量，影响力至今巨大，随时准备“因时而动”，重夺其在中国的文化霸权。正是这股力量，勾结邪恶的共产党政权，正在阻挡唯一能拯救我们中国人的“全盘西化”大潮，并在共产党的政权支持下，儒教这个思想界的邪恶力量不但卷土重来，而且愈发强大。在中国，儒教已经以“文化保守主义”的面目卷土重来，其文化特务刘军宁，康晓光，杨恒等更是混入民主阵营，炮制“儒家宪政主义”意欲伺机夺取民主果实！蒋庆的“尊孔读经”和周锋利的“抵制西方节日”在政府的支持下更是大行其道！这股逆势而动的狂潮，正在以“复古”（复孔）为名，以“国家，民族”为旗，以维护共产党专制独裁为目标，以剥夺每一个中国人作为“个人”的“自我”，蛊惑中国人放弃中国人对自身价值的追求，放弃中国走上真正的“平等，民主，自由”道路，重回儒教思想统治时期的蒙昧，愚顽，重回集体主义时代，过上“轻个人而重集体”的每“个人”的悲惨生活。<br />
