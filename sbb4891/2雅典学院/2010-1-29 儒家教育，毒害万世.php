为什么儒家教育危害如此之烈？我们的教育，应该教给孩子们“学说话，学办事，学思考”。而其中，最基础，也是最重要的，就是“学说话”。只有学会说话，我们才能从一次次的辩论，交流中，获得逻辑，获得“做事，思考”的工具――逻辑。同样的，人拥有了逻辑分辨能力，能够独立思考，他们就会明白各自应该做什么，也就是“会做事”。这，才是教育的真谛。<br />
而我们中国呢？统治者“独尊儒术”。实质上就是将我们的教育限定在了儒教教育的范畴内，换句话说，中国的教育，长期以来是按照儒教的方向发展的。并最终导致中国的教育彻底的扭曲，毒化。中国的教育，变成了把人们教傻，教蠢，教坏的邪恶洗脑。最终呈现在世界面前的，就是鸦片战争时期的“野蛮中国人”，就是当代的“丑陋中国人”，就是“黄祸”！<br />
教育的基础是“学说话”，让我们看看儒教对待这一基础的态度：<br />
1.禁言.孔子的名言是“非礼勿言”。不要相信当代中国儒教徒们出于其邪恶目的对“礼”的解释。孔子的礼，指的是“周礼',周礼是周朝的等级制度，严格的规定了人的等级性，所以，孔子的”非礼勿言“就是要求人们按照等级说话，用”礼“，来禁止人们的言论自由。一个反对言论自由的教育，会教育出怎样愚顽落后的学生，不需多言。更何况，孔子率先开创了”因言获罪“的恶劣先例，为日后的满清文字狱奠定了历史和思想的基础。“言伪而辩”、“记丑而博”，即是孔丘杀害少正卯的刑事判罪的最重要的依据。<br />
2.畏言。孔子主张”畏言“。孔子的名言是“畏大人，畏圣人之言”。什麽是“畏”？“畏”就是害怕，恐惧，人们在恐惧的心理状态之中怎么可能自由地正常地交流？<br />
3.孔子主张”隐言“。也就是公开的教导学生学会，且必须撒谎。孔子的名言是“臣为君隐”、“子为父隐”，很显然，孔丘主张，为了“君”的利益，为了“父”的利益，也即为了“亲亲尊尊”的“礼”的利益，人们应该蓄意隐去真实，说白了，是公然主张“说谎”、“做假证”。 <br />
