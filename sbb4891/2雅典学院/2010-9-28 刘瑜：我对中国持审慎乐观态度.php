													<h2>刘瑜：我对中国持审慎乐观态度</h2>
						
						
																											<div id="postmessage_847826" class="t_msgfont">南方人物周刊<br />
<br />
<a href="http://www.bullock.cn/blogs/nfrwzk/archives/132944.aspx" target="_blank">http://www.bullock.cn/blogs/nfrwzk/archives/132944.aspx</a><br />
<br />
特约撰稿 刘天昭<br />
<br />
今年8月，刘瑜从英国回到中国。她辞去了剑桥大学的教职，进入清华大学人文社科学院政治系任教。即使人在国外时，刘瑜也已经是国内公共领域最重要的声音之一。她为媒体撰写的政治评论专栏既受好评又受欢迎，去年结集成了《民主的细节》，上市不到一个月就加印，年底入选各大好书榜、畅销榜，截至2010年8月印刷18次。<br />
<br />
学历过度豪华的刘瑜，以简单的真诚，几乎是下意识地，超越了学术著作和通俗读物的界限、精英和群众的界限，做到了真正的深入浅出。她无意启蒙，她的书却无疑是近年最好的启蒙读物。<br />
<br />
刘瑜的很多读者，更热爱她的生活博客和偶尔为媒体撰写的文化专栏及小随笔。小侦探一般的超级粉丝，可以从中拼凑出一个生活化的刘瑜，情况也基本属实。但刘瑜说自己这几年变化挺大的。一个可观察的变化是，她现在不怎么写自己的生活了。可能因为读者越来越多，写那些不合适了，这算是一个公众人物的自觉；也有可能，对外在世界的好奇心和求知欲，作为她喜爱的、鼓励的那部分自己，正在快速地膨胀，压倒性地统领她的国度。提起计划中要写的论文、书和要做的研究，她就真的非常兴奋，十分期待能早日扫清障碍大干一场。听她那语气，你就能提前感觉到热火朝天。这可真让人替她高兴。<br />
<br />
她说她仍然，间歇性地热爱生活，间歇性地厌世。那最好当然是，前者把后者挤出去，或者留下少量的忧郁--可以轻轻陶醉的那种。微醺就好。如果真能这样，对她自己来说当然是件大好事，一个人从里往外地灿烂。如果真能这样，对她那些读者来说，更是件大好事--刘瑜专心致志地做一件事，那到底会是个什么效果？<br />
<br />
不是启蒙，是自我教育、分享精神<br />
<br />
人物周刊：对专栏以及后来结集成的书如此受欢迎，感到吃惊么？<br />
<br />
刘瑜：不是特别吃惊。我觉得自己在适当的时候和地点写了一本适当的书。中国在一个转型期，政治上很多人都很迷惘，我针对一些令人迷惘的问题，分享了一些我的看法。<br />
<br />
人物周刊：把目标读者具体化为一个人，能够描述一下这个人么？<br />
<br />
刘瑜：我一般会把我比较喜爱和尊重的人当作假想读者，就像我备课时会把班上最优秀的学生作为假想听众一样。<br />
<br />
人物周刊：写这些文章的时候，你有自觉的启蒙意识么？<br />
<br />
刘瑜：还真没有，很多时候是自我教育，还有些时候是分享精神。<br />
<br />
人物周刊：在什么意义上使用自我教育这个词？<br />
<br />
刘瑜：自我教育就是试图理解你所置身的这个世界，如果能通过理解这个世界树立更正确的价值观，当然就更好了。<br />
<br />
人物周刊：你会不会觉得启蒙这件事，有一点伦理上的尴尬？<br />
<br />
刘瑜：是，一个人得多自信才会那么斩钉截铁地居高临下啊。我没有那种自信。我们都是“红旗下的蛋”，既然如此，自我教育和教育他人只能同步进行。<br />
<br />
中国的市场化不是那么非黑即白<br />
<br />
人物周刊：谈谈对中国转型的基本看法。<br />
<br />
刘瑜：我对中国持有审慎的乐观态度，觉得从长线来看，在向一个更开放、文明的方向前进。<br />
<br />
人物周刊：短线来看呢？比如10年。<br />
<br />
刘瑜：在这个问题上，我觉得很难说有一个笼而统之的中国，从社会活力上来说，我觉得它在不断进步，从政治上来看，进步可能慢一些。但政府没有主动进步，也可能在社会的推动下有被动进步。这是很多转型国家的经历。<br />
<br />
人物周刊：社会进步和政治慢慢进步，这个差距会不会造成风险？<br />
<br />
刘瑜：会。你看中东欧国家80年代的转型经历，就会发现越是拒绝改革的国家，比如罗马尼亚，最后转型的方式越惨烈，越是“把握时代潮流”的国家，比如捷克、匈牙利，转型越是相对平和。我觉得中国应该从自己的历史伤疤和他国身上学到足够的经验，防止这个矛盾激化到暴力冲突的地步。<br />
<br />
人物周刊：除了暴力冲突，历史和他国经验中，有没有其他扭曲或者病态？避免暴力冲突是否就能避免走进新专制的怪圈？<br />
<br />
刘瑜：当然有，太多扭曲和病态了，即便是中东欧的和平变革，也经历了10年的经济动荡和萧条。陷入新专制怪圈也不少见，比如俄罗斯。中国相比它们有自己的优势，也有劣势。优势是中国市场经济改革已经十有八九，社会多元化也相应出现，不用像中东欧国家那样政治、经济、社会同步变革，那样造成的冲击太大。劣势是中国的专制传统深厚，而且制度的缝隙比很多其他转型国家要小。<br />
<br />
人物周刊：这个专制传统，具体体现在什么方面？<br />
<br />
刘瑜：比如体现在对权威、当权者的莫名敬畏和习惯性服从里。在习惯性服从权威的同时，转过身又对无权无势的人颐指气使。<br />
<br />
人物周刊：这个所谓文化，是有传统，应该也是当下现实激励的结果--解毒力量在哪里？你刚说看到社会在进步包括这方面？<br />
<br />
刘瑜：最大的解毒力量可能是市场经济，当社会和经济资源越来越少地为权力垄断，人们对国家的依赖性就会减弱，一旦物质依赖性减弱，精神自主性也会增强。这和女性经济独立后不那么听男人的话了是一个道理。然后就是公共领域的拓展，市场化的媒体和出版业、民间组织、互联网的发展，使得以往一个个孤立的人有了凝聚的可能性，而且是一种横向而非纵向的人际网络，这对消解那种“威权人格”应该也有好处。用学者普特南（Putnam）的话来说，这就是“社会资本”，即使这些民间网络不关心国事天下事，仅仅是组织旅游俱乐部、羽毛球俱乐部，都是在积蓄“社会资本”，都是在为良性政治添加润滑剂。<br />
<br />
人物周刊：你刚才说到，中国的市场经济已经改得十有八九了，这个判断是怎样生成的？这几年国进民退，即使只从增强国民依附性的角度讲，这是否也是一个退步？<br />
<br />
刘瑜：十有八九这个词可能不确切，说十有五六也行，我的意思是在经济改革中，市场化转向的最关键一步已经迈出，“市场”这个词已经非常深入人心了。至于“国进民退”，这个媒体上现在谈得很多，但说实话，我觉得这是一个需要足够宏观数据支撑的判断，不知道是不是正确，因为我读到过正反双方的数据。也许这个话题这么热，表达的仅仅是大家对经济改革速度放缓甚至陷入停滞的不安。当然，即使仅仅是一种“感觉”，这种感觉本身也是社会力量觉醒的一种表现。<br />
<br />
人物周刊：即使没有国进民退，普遍化的所谓裙带资本主义也是一个不争事实吧。如果市场化就只是裙带资本主义化，有没有可能事实上增强人们对权威、当权者的敬畏和服从？<br />
<br />
刘瑜：我觉得中国的市场化不是那么非黑即白，很多企业未必只靠裙带，也许一边靠裙带、一边也要靠提高产品和服务质量。比如我发现现在去银行办事，服务态度一般都很好。总的来说，我相信市场经济所蕴含的契约精神、竞争意识长远来看是对专制文化最好的解构力量，当然这肯定是一个漫长的过程。<br />
<br />
人物周刊：关于公共领域的拓展，很多人担心在目前制度环境下，拓展到一个程度之后，就拓不开了。所谓精英与最大多数的大众之间的沟通渠道非常狭窄，并且只能以很间接很暧昧的方式进行。<br />
<br />
刘瑜：我也觉得在现有制度条件下公共领域的拓展比较缓慢，但方向应该不会变。别忘了历史的改变往往不是匀速的，可能是20年缓慢地变化，然后三五年迅疾地变化。对比勃列日涅夫时代和戈尔巴乔夫时代就能看到这一点。而且，时代的缓慢变化未必是没意义的，它也是一种积累，甚至在一定程度上，激变需要一个渐变的铺垫和准备，这样当激变来临时，社会是在有相对共识的情况下迎接这个变化的。<br />
<br />
经济的发展和开放推动观念的变化<br />
<br />
人物周刊：让你感到社会在不断进步的因素有哪些？个人经验的、调查研究的，分别讲讲。<br />
<br />
刘瑜：个人观察到的很多。比如，20年前市场经济这个词还是禁忌，1997年十五大时，还在说要保障非公有制企业的合法地位；现在，媒体讨论的是“凭什么国企拿地”、“凭什么垄断企业工资高”，问题意识的边界一直在移动。这本身就是一种进步。<br />
<br />
另一个理由是对他国经验研究的阅读。比如学者Iglehart用民意调查方式，20多年跟踪研究几十个国家政治观念的变化，对“经济发展-观念变化-制度变化”这3个变量的关系得出了令人乐观的结论。读到更严谨和有说服力的研究之前，我暂时相信他的结论。关于中国，也有一些类似的研究，结论也类似。<br />
<br />
人物周刊：我对“历史必然性”这种东西感到怀疑。<br />
<br />
刘瑜：我也不相信历史必然性，我只是说如果经济继续保持发展和开放的话，文化和制度会相应变化。但那是很大的“如果”，不排除资源能源环境甚至政治等因素可能颠覆这个“如果”。总体我还是乐观，这种乐观的根基可能是我相信自由的制度安排不单在道义上是好的，而且在绩效上是最有力的，所以即使对人的善意失去信心，也不应该对人的逐利倾向失去信心。只是“必然”这个词太绝对了，我这种天生的怀疑主义者不会使用。<br />
<br />
人物周刊：经济发展导致观念变化，这样宏观去讲基本上不会有争议，不过具体点说，这个传导链条里有哪些关键环节？<br />
<br />
刘瑜：大体来说，经济的发展和开放推动观念的变化，无非是几个机制：第一，当经济发展到一定程度，会有更多资源流向“基本生存”以外的领域，其中一些会流向公共领域。比如我认识一些商人，他们开始有“余钱”资助一些论坛、出版之类的事情，普通人也会有更多的钱来买书、上网、捐赠慈善事业等等；第二，经济发展之后，交通、通讯方式更发达，有利于人们建立更密集的横向交流网络，而在交流中可以“撞出更多思想的火花”，网络论坛、博客、微博都如此；第三，市场经济的契约精神会渗透到文化里，增进人们的权利意识；第四，这是 Inglehart的观点，就是随着后工业时代的来临，越来越多职业和工种要求人们作出个体化的判断，而不能像机械流水线上的“螺丝钉”那样工作，这也鼓励人们的独立自主思考能力。至于这些机制如何在千千万万人身上起作用，似乎不可能一一分析。<br />
<br />
人物周刊：在你的判断中，中国人在观念变化这一步，走了多远？<br />
<br />
刘瑜：这一点Inglehart、杜克大学的史天健老师、台湾的朱云汉老师等都做过很规范的民意调查和分析，也跟踪了有些年头。基本上结论大同小异，就是随着经济和教育的发展，人们对民主的支持和理解、政治宽容度等都向普世价值靠近，虽然还有差距。至于具体“走了多远”，要是感兴趣，可以去搜他们的论文读。不过由于调查研究的滞后性，他们的很多文章数据都来源于2005年以前的调查，现在的民意变化应该更进一步了。<br />
<br />
为什么会出现狂热而真诚的政治参与<br />
<br />
人物周刊：在国内做研究的优势和劣势分别是什么？<br />
<br />
刘瑜：如果是研究中国的话，优势很明显，就是你离“现场”很近，能即时听到各种声音。我跟朋友说，回国以后，我每个饭局都是一次“田野调查”。而且现在由于网络发达，即使在中国，你要接触到国际上最前沿的研究也很容易。劣势也很明显，国内学术研究和对话的水平比较初级，常常还是一种不讲论据的“侃大山”风格；另一个极端就是用故弄玄虚的“学术黑话”来包装空洞，如果我不知不觉把要求自己的标准换成这些坐标系可能就“迷失自我”了，所以我现在面临的挑战之一就是如何保持适度的孤独。<br />
<br />
人物周刊：大家只知道你是政治学学者，具体的研究领域是什么？<br />
<br />
刘瑜：之前一直是研究中国革命中的动员机制问题，最近对比较政治转型问题感兴趣，做了一些阅读，准备写点系统的东西。<br />
<br />
人物周刊：你在博客上透露过要写一本《中国革命十论》，能不能提前介绍一下核心观点。<br />
<br />
刘瑜：核心问题是“为什么中国革命中会出现那样狂热而真诚的政治参与”，核心观点用“极简”的话说，就是利益激励导致的政治参与可能因为人类对“认知一致性”的心理需求而转化为出于信念的参与。我既想避免那种把民众当作“投机分子”的看法，也想避免把他们当作“纯洁信徒”的看法。当然这只是极简的说法。<br />
<br />
人物周刊：如果人类对“认知一致性”的心理需求客观存在，怎样才能避免这心理被政治野心家利用？<br />
<br />
刘瑜：解除民众对国家的经济依赖以及职务、声誉依赖，其实也就是哈耶克说的，没有经济自由，不可能有真正的政治自由。<br />
<br />
人物周刊：你是从哪一个角度关注政治转型的？<br />
<br />
刘瑜：两个角度：一个是政治转型的动力何在；一个是为什么那么多国家政治转型受挫，比如俄罗斯的新专制怪圈，或者民主化过程中族群矛盾的激化。我对后一个问题更感兴趣一些。<br />
<br />
人物周刊：他国经验、历史经验、其他学者的实证研究，与个人经验冲突的时候，会试图用前者诱导性地说服后者么？<br />
<br />
刘瑜：会，但有时也会试图用自己的经验去反思一些理论。人或多或少都有意识形态，但一个骄傲的人会觉得智识上的诚实比什么都重要，主要是你舍不得放弃翻阅一波三折的世界的乐趣。<br />
<br />
发现互联网，“吞下那颗红药丸”<br />
<br />
人物周刊：中学时对什么东西真的感兴趣？<br />
<br />
刘瑜：虽然在我幽暗的中学时代有过热爱崔健这样的传奇经历，但总的来说我的中学时代特别平淡无奇，基本是个学习机器。我知道很多有自由派倾向的人中学时代就有过种种叛逆行为和思想，我中学却几乎没有任何愤青迹象。<br />
<br />
人物周刊：大学期间感觉到学习乐趣了么？<br />
<br />
刘瑜：也没有，但至少知道了学习之外另有天地。比如为了当上一个合格的文艺女青年而读萨特、尼采、海子什么的。<br />
<br />
人物周刊：你的意思是，不是真诚地读？没有受到影响？<br />
<br />
刘瑜：没有持久的影响，可能当时也会搅动小心脏一下。如果说有影响，甚至有点坏影响。我觉得90年代自己成长在一个特别恶劣的文化环境里，前半段还是萨特、海子什么的，后半段就是福柯、罗兰・巴特什么的。我对这些人物本身没什么意见，但在当时中国的环境里，他们往往是以这“热”那“热”的形式出现，尚未有强大自我和判断力的孩子，很容易就把他们当时髦来赶而不是思想来读，结果造就了一批迷恋姿态远甚于思考本身的青年学子。<br />
<br />
人物周刊：听起来小时候很乖，长期尊重环境提供的评价标准，后来，真诚的勇气、自我肯定的勇气，是从哪里来的呢？<br />
<br />
刘瑜：可能前面对中学大学时代自己的描述不大公平。我中学也组织诗社、大学也写小说什么的，一直隐隐有种“要和别人不一样”的冲动。但到90年代末期，还是只有“文艺”的那一面被开发出来，一直没有碰到合适的思想资源浇灌另一个充满好奇心和求知欲的自己。<br />
<br />
人物周刊：后来碰到什么合适的了？<br />
<br />
刘瑜：我觉得网络对我是个巨大的冲击。我1999年左右开始上网，稀里糊涂就上了时政论坛，我被有些人蛮不讲理、是非颠倒的辩论方式给吓坏了，也给气坏了，正义感一下就给激发起来。然后这种正义感就开始感召我读一些历史性的东西，而不是萨特、福柯这样时髦但实际上跟我的生活和感受没什么关系的东西，结果一根线越扯越长，扯出很多让我受震动的东西。博士论文写中国革命就发源于网络辩论。<br />
<br />
而且，另一方面，网络辩论的即时性和直接性也训练我以严密的语言和逻辑思考--因为你的论敌不断迅速指出你的这个或者那个漏洞，你不得不不断调整自己的观点和逻辑。所以我现在思维里很重要的两种东西，对论据的重视和对逻辑的重视，都起源于当时的网络辩论。基本上，发现互联网对我来说就是《黑客帝国》里“吞下那颗红药丸”的片刻。<br />
<br />
人物周刊：这个好东西留学期间加强了吧。<br />
<br />
刘瑜：对。我记得刚到美国那个圣诞节，当时的男朋友从西岸来纽约看我，发现我没日没夜“战斗”在网络上，不怎么搭理他，伤心坏了。基本上我留学第一年除了应付基本的学业，就是战斗在网络上。不但战斗，还转战不同的论坛，简直是游击队员。<br />
<br />
人物周刊：相比之下，国外大学的教育，反而没有那么大的影响？<br />
<br />
刘瑜：国外对我的影响主要还真不是来自校园，虽然我在哥大期间学习成绩很好。主要是当时的课程内容（其实还是一些预设你已经熟知历史和经验世界的理论）和我当时的问题意识（主要是对中国当代史的兴趣）不衔接。不过国外生活最后对我还是影响巨大，主要是两个途径：一个是对他们的社会和政治生活的观察，这个《民主的细节》一书已道出；另一个就是后来我按照自己兴趣进行的阅读，尤其是在剑桥这3年。因为自己做了老师，终于不用活在老师的指挥棒下，可以按照自己的兴趣阅读，就跟发现了一个金矿一样。<br />
<br />
间歇性热爱生活与间歇性厌世<br />
<br />
人物周刊：还有文学梦么？<br />
<br />
刘瑜：当然有，不同的文字形式可以表达人不同的侧面，一个丰富的人不愿意放弃自己的某些侧面。我没有文体歧视，有些人觉得论文比随笔高级，小说比专栏高级，我不觉得。我愿意从各个角度挑战自己。<br />
<br />
人物周刊：你以前提过，想拍电影、导演话剧，死前一定要做的事，列了单子没？<br />
<br />
刘瑜：估计3年内是没有时间去搞文学了。拍电影、导演话剧是技术活，我是过了那个村了。但写点小说随笔什么的，技术门槛比较低，有时间肯定还会去做。死前要做的事清单太长，已经排到第八辈子了。<br />
<br />
人物周刊：真是热爱生活啊。<br />
<br />
刘瑜：间歇性热爱生活与间歇性厌世此起彼伏，呵呵。<br />
<br />
人物周刊：你对自己的间歇性厌世，有好奇心么？你理解了自己么？<br />
<br />
刘瑜：起先有，特别是年少的时候，觉得谁虚无谁酷，谁整天乐呵呵的太“三俗”了。后来发现虚无这事其实也没什么可拽的，它不就是个虚无么。我是喜新厌旧的人，包括对自己的情绪。可惜在我已经厌倦了那个幽暗的自己之后，它还是没有离去，所以有时就是漫长无聊的僵持。<br />
<br />
人物周刊：发现自己的好奇心和求知欲之后，是否逐渐产生一种人生借此安顿的感觉？你的生活是否仍然存在彻底被颠覆的风险？向哪些可能性开放？<br />
<br />
刘瑜：希望至少能安顿个三五年吧，之后怎样只能走到哪算哪。虚无主义的危险肯定一直在那儿，但不想渲染它，希望通过忽略它而被它忽略。<br />
<br />
人物周刊：在茂盛的智力生活之外，最主要的生活内容是什么？<br />
<br />
刘瑜：基本上我是个没有生活的人。爱听音乐。网上看各种八卦。以前还爱逛街买东西，后来发现家里不用的东西比有用的东西还多、不穿的衣服比穿的衣服还多，就不爱逛街了，改喜欢扔东西了。<br />
<br />
人物周刊：觉得幸福么？<br />
<br />
刘瑜：读到一本好书，听到一段好音乐，或者买到一只没有倒瓤的西瓜，这样的片刻，还是挺幸福的。<br />
<br />
人物周刊：假想，你这样一套基因，投胎到美国，一个中产阶级家庭，最主流的那种，你会长成一个什么样的人呢？<br />
<br />
刘瑜：我估计还是现在这个样子，只是少拐30年的弯而已。</div>

							
							
							
							
