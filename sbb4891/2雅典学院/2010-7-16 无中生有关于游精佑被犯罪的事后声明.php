<br />
一个月前的3月19日，那个一分钟的庭审，使我感受到的不是法律和司法的威严，我感受到的是司法的阴谋和暴戾！司法是要维护当事者的尊严，从而也维护法律的尊严，如果司法是刻意的侮辱---漠视侮辱当事人，漠视侮辱辩护人，它同时也就侮辱了法律。执行这个被侮辱了的法律的所谓执法者，他能感受得到自己的尊严吗？我感受到了羞辱和愤怒，刻意激起一个公民的愤怒甚至仇恨，这正是你们想要达到的目的吗？<br />
<br />
我想对那天阻止一个女儿拥抱她的父亲的那个中年妇女说：我不知道你的姓名，也无所谓。你的子女还健在吧，你知道我的女儿过两个月就要高考了吗？你以为你穿着制服就可以展示自己毫无人性的一面吗？纳粹也是穿制服的，而且比你的制服更威风。<br />
<br />
我想对我的女儿说：孩子，现在我的心里已经没有忧伤了。这场司法闹剧使我更深刻地认识了人性。当你哀求别人的时候，要看对方是否铁石心肠，是否有基本的人性，所以，不要哀求他们，他们不配。<br />
<br />
我想对公诉人陈潼先生说：就这个案子，你对法律的想象力使我惊诧。我的视频有多少人在看？你举证了那篇文章有多少人在看！我的视频有没有捏造事实？你举证了那篇文章捏造事实，那是怎样的捏造阿！我的视频里林秀英有指控那几个所谓的受害者吗？质疑等于指控吗？你举证了文章里有所谓的指控。另外，你那天还拍着桌子引用我的聊天记录，说我要和范燕琼联系，请问：我跟她联系了吗？你还说我提到视频如果不抓紧做，以后就没有影响力了。请问：在这片国土上，每天网络上要发生多少事？如果没有这场司法闹剧的炒作，有几个人会知道那段视频，会知道林秀英女士的口述会是什么样子的。<br />
<br />
公权机关的形象受到损害？质疑会导致公权机关的形象受损吗？这是什么逻辑什么想象力！你列举了我在口述影像中所做的事，有哪一点能证明我引用了这篇文章？就是那几秒钟的一个镜头吗？这是什么法理逻辑？我在此想跟诸位复习一下刑法第二百四十三条：捏造事实，意图使他人受到刑事追究。<br />
<br />
我有捏造吗？我有意图吗？相反，11月11日。在回答关于林秀英女士为何不能出庭作证，以及为何未被追究的问题时，你说林秀英只是在遇到我们三位时，才想到了对有关人员进行刑事追究。而在你的起诉书中白纸黑字地写着：林秀英遇到范时，明确要求严惩凶手。请问，陈先生，你何苦要捏造我们教唆林秀英的事实？目的只是意图使我们被追究刑事责任吧！我看这很符合诬陷罪的构成要件呢！审判长，你认为呢？<br />
<br />
温家宝总理在今年的3月份说过：政府要创造条件接受百姓的监督，他老人家还说过：公平和正义比太阳还要有光辉。<br />
<br />
听听他的话，看看还在这里发生的事，请你们摸一下良心----如果你们有的话，这里发生的事情是否都是在一而再地嘲弄这位善良可爱的老人家呢？<br />
<br />
快十个月了，我和我的兄弟姐妹们在等待你们给我的说法，我，游精佑，肯定也会给你们一个说法的。<br />
