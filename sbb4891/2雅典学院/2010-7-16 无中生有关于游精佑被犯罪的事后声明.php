													<h2>一位父亲的忧伤――致豫Z</h2>
						
						
																											<div id="postmessage_744498" class="t_msgfont">孩子，七月五日之后四个月又六天，我们才再次见面，虽然是在法庭，我带着手铐，穿着11号的号衣。但我还是感到幸福。当时我只是紧抿双唇，远远地对着你们母女俩点着头。你们被安排在那一大帮身份不明的小伙子中间，泪流满面。庭审过程中，我数次回望，都能看到你的口型不断地在重复着这句话：我没事，我没事。<br />
<br />
&nbsp; &nbsp; 我的心里很平静，即使是这样远远地相望，我的心里确实很平静。<br />
<br />
&nbsp; &nbsp; 每次休庭回待审室的时候，你都很聪明地挤到前排，到栏杆边。我们都抓住了机会，我拉住了你们的手，亲吻你的脸颊，听着你双眼含泪但依然笑着轻声说：爸爸，我没事。<br />
<br />
&nbsp; &nbsp; 在我抚摸着你妈妈的手的时候，我想说：你们都瘦了。只是我说不出口，我害怕掉眼泪。我不认为掉眼泪是件羞耻的事。但是，我不想让他们误会：以为我是想引起同情，有误解的同情，咱们不需要。就像我对《起诉书》的意见：你可以用事实指控我，但不要用卑鄙这一类词汇侮辱我！不管法律被怎样肆意妄读，法庭被怎样刻意安排，我们都要在这个平台上保持尊严。我坚信，进步是需要平台的，即使这平台被扭曲走样。<br />
<br />
&nbsp; &nbsp; 那天清早，押送我的车子开出看守所的时候，我看到前面有警车开路。到路口，还有另一部警车在那里封路。他们这么慎重，只是要确保一切按既定安排进行。车子走高速公路，刚进入法院大门时，我看到聚在门口的姑姑们，她们望眼欲穿地伫立着，只想看看她们日夜牵挂的兄弟一眼。但这也不能如愿。她们未被允许入庭旁听，因为坐席已被刻意安排的小伙子们占满了。而他们不是要看我，也不是要在这里见证法律被怎样执行。他们只是被安排的看客，旁观，而不是见证，这样才会确保审理按计划进行。<br />
<br />
&nbsp; &nbsp; 但你是见证者，孩子，那个叫游精佑的在被告受审，但你老爸我也是见证者。我们共同见证了这个体制的法律进行的最后一道程序。见证了国家机器精心配合捉弄一个公民的闹剧。这不是很有意义的事情吗？<br />
<br />
&nbsp; &nbsp; 据说麻醉有一种奇怪的现象：一旦麻醉生效，你就感受不到疼痛，但你能感觉到刀子一下一下地划在身上。我感受到了这把刀子，很脏。但我很庆幸，几个月以来，我的表情没有扭曲，否则，在你面前我会感到羞愧的。<br />
<br />
&nbsp; &nbsp; 我总在想，总会有疲惫的时候吧，他们的耍弄总会有疲惫的时候吧。那时候我就可以全身心的感受你们的爱，感受到只要我们能坚持，垮下的肯定不是我们。<br />
<br />
&nbsp; &nbsp; 我要谢谢你，我的孩子，在我被迫离开你们的时候，没有我的陪伴和照顾，你依然能独自坚持，一个人在外地继续高考前的冲刺。你那么年少，却又如此坚强，爸爸为你的表现感到自豪！<br />
<br />
&nbsp; &nbsp; 你我认识刚过十六个年头（这是借用你的说法），我一直对你吝于表扬。那与其说是作为父亲的责任，不如说是我们父女的交往游戏。我只想默默地注视着你成长的每一步。我常常因为你感到幸福，只是没来得及像现在这样说出来而已。<br />
<br />
&nbsp; &nbsp; 记得还是在你初三的时候，我们一家正吃着饭，你滔滔不绝的历数一周来自己在学校的表现和老师们对你的肯定。我和你妈妈只是不动声色地吃饭。见我们没有附和，你叹了口气说：“哎，在家里没人表扬，只能自己表扬自己咯。”孩子，其实我们当时心里都很高兴，没表露出来只是因为大人虚伪的谨慎。你知道吗？如果我们为你感到自豪，那未必要你表现优秀，而只是因为你很真实，不自私，有同情心！还有，你是我们的女儿啊！<br />
<br />
&nbsp; &nbsp; 你妈妈告诉我，在你很小的时候，有一次寒潮来临，你幸福地偎再在她的身旁，说：“天这么冷，那些乞丐怎么办？”我还记得另外一件事，依然是你小时候，有一段时间你不爱吃鱿鱼，受不了妈妈的逼迫，就找我投诉说：“我觉得妈妈很残忍，那些鱿鱼本来活的好好的，在水里游来游去，她却要把它们杀了，煮熟了，还一定要让我吃。”<br />
<br />
&nbsp; &nbsp; 庭审结束已经晚上九点多，你知道吗？那天我看着门外水泥地上肆意溅落的雨点，天色越来越黑。我想着你晚上还有两个半小时的车程，要独自返校，忧伤压得我喘不过起来。我只希望那肮脏的刀子快点落下，早点结束。<br />
<br />
&nbsp; &nbsp; 回到看守所，我才看到了你十几天前寄来的信，里面有你写的《我的父亲》，你最后那句话说的真好“正义从来不是私有品，而是共有财产”。你才刚满十六周岁，我不知道你小小的脑袋里怎么会有这样的归纳。爸爸平时想这些想的不多，爸爸只想着欢乐是人生的追求。但总有那些忧伤让我揪心，那些人们看得到的人造苦难。欢乐未必都是正义的，可是为什么人为的不正义就不能尽量少一点呢？<br />
<br />
&nbsp; &nbsp; 忧愁和痛苦也是人生的组成部分。我们所能做的，是努力去减少人为因素造成的悲剧，你说是不是？爸爸只想像蚂蚁一样，不断地，力所能及地搬运自己能搬得动的东西。给同伴，给自己，同时也给你，我的朋友，我亲爱的女儿，搬运清理掉一些障碍物。一想到你可能还要重复承受我正在经受的谎言和侮辱，我就觉得难以容忍。人生就是这样一茬一茬地接续下去的。<br />
<br />
&nbsp; &nbsp;谢谢你在信地末尾的那句话：在别人手里，为自己活着。高墙内外的我们都在别人手里，但我们都有权利知道，也有权利高声说：“我们要为自己活着。”现在的我并没有感到冤屈，虽然我有被侮辱，被肮脏地无理纠缠的感觉，但我是在为我自己活着。<br />
<br />
&nbsp; &nbsp; 在法庭的最后陈词中，我对你说不要绝望。我的意思不是要你对判决结果抱有太大希望，而是要你不要对社会对人生绝望，不管对你，对爸爸我来说，我们都有很多理由伤心，甚至也有克制不住的愤怒。但是，亲爱的孩子，任何情况下，我们都不能绝望！<br />
<br />
&nbsp; &nbsp; 祝福你，我的孩子，祝你高考顺利，爸爸时刻都在为你祈祷。这是我目前唯一能尽得作为父亲的责任。愿主保佑我们平安。<br />
<br />
