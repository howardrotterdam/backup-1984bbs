																											<div id="postmessage_742983" class="t_msgfont">Fairplay（游精佑的二审陈词） / 无中生噎<br />
2010-07-14 10:49 | 阅读(408) | 标签: 无中生有, 天理, 诽谤或诬陷 | 字号：大 中 小 <br />
<br />
让我们省点精力吧！<br />
<br />
我不想就一审判决书中所认定的事实做过多的解释，只想要求请陈仰东出庭证明一下，在拍视频前我有没有跟林秀英说她“可能会被灭口”。我知道法庭还是不会同意让林秀英出庭作证。那么，是否可提请你们同意补充一份笔录：问林秀英兄妹，我有没有跟他们说过类似的话？提这件事是想让二审法庭注意，本案关键证据，包括指控范燕琼捏造的部分都是孤证。在这种情况下，我认为证人的出庭非常重要，但一审法院不允许，他们在怕什么？<br />
<br />
我认为我自己所涉及的案情极为简单：在几个朋友的帮助下，我帮林秀英女士做了一段24分钟左右的视频。在视频中，林女士根据自己的所见所闻，讲述了她女儿死亡事件的过程，提出了她的质疑。在视频的最后，依次出现了范燕琼女士第一篇网文首页的镜头，以及林秀英上访材料的首页的镜头。这些镜头只出现一次，并未反复闪现。在视频的最后，有一个我自己并不完全认同上述观点的声明。但这个声明一直被侦查单位，监察部门，和一审法庭所刻意忽视。<br />
<br />
一审法庭认为将可能存在捏造内容的文章制作进视频。（请注意，只有几秒钟，只出现一次的镜头，这是符合“制作进”的要件的，但这个“制作进”却会给未看过视频的公众什么样的暗示呢？）是我定罪的关键。这个“制作进”就是我“捏造”或“参与捏造”吗？那么，它们是否认为林秀英讲述的24分钟的内容都是真实的？她如果有捏造，那我该怎么被定罪？最关键的是：记录，传播是否是等于捏造？没有捏造，是否依然可以使本案的诽谤罪成立？上诉状中我们对这些观点也已有阐述，请二审法庭能帮助我们厘清这里面的法理关系。<br />
<br />
一审法庭认为本案案情复杂。是不是因为案卷有十几本，案情就复杂了呢？马尾公安扣押至今未还给我的物品中，有一个200G的移动硬盘，那个硬盘储存的是我2007年前的资料，那时候，严晓玲还是个活生生的生命。如果按这样的逻辑侦查，这个侦查案卷可以整成一百本！是不是会更复杂？审理时间是不是还可以拖得更长？而我只是做了个24分钟的视频，一个一百兆左右的文件；一审法庭认为本案案情复杂，是否是还因为本案“受害者”众多，“社会影响”巨大？但第一次开庭时，控方并未提出需补充资料，要延期审理；第二次的开庭，头尾只花了一分多钟的时间，庭上只做的一件事是：要补充受害者资料，建议延期；第三次开庭时，控方宣读了十几份补充侦查的受害者资料。让我吃惊的是：其中有部分是第一次开庭和第二次开庭之间的资料。我很困惑：这些资料是3月19日之前就做好的，还是3月19日后做好的补做的？如果是3月19日之前就做好的，为什么第二次开庭时不公开宣读？动用那么多警力，开一次庭容易么？如果是3月19日之后补做的，那么，取证的时间都可以作假，这是什么性质的行为？这样的证据有什么意义？一审法庭居然采信了有关证据，这说明了什么？在审理阶段，法庭可以和控方勾结成奸吗？还有，补充资料中的几位受害者在起诉书中并不是受害者，这是法庭指示它们罗织的吗？<br />
<br />
温家宝总理说过：公平和正义比太阳还要有光辉。我认为法律是守护社会公平正义的最后一道防线。这个底线一旦被突破，被践踏，对我们这个社会，将意味着什么？这个案子是否已成为某一领导用以检验自己对福州司法统治效率的试验田了呢？<br />
<br />
南非世界杯马上就要举行，不知大家有没有注意到，世界杯时，每场比赛，都有人举着一面旗帜，上面写着：Fairplay。意思是：讲公平，讲正义，讲原则。<br />
<br />
鲁迅先生也曾写过讨论Fairplay的文章。二三十年代的音译比较低俗，先生把她直译成“费尔泼赖”。足见他对当时社会Fairplay被扭曲的愤慨和痛心疾首。<br />
<br />
我的大学英语老师告诉我，这个词是林语堂先生造的，林先生和鲁迅先生是同时代的人。他不是中共党员，后期在美国当寓公，我的老师说他的英文比中文还好！我看过林先生的小说，和一本叫《中国人》的书。作为中国人，却创造了Fairplay这个外文词，也足见他对公平和规则程序的渴求，足见林语堂先生对我们中国潜规则文化的厌恶，对公平的呼喊！<br />
<br />
书记员，作为中国人，写英语总不习惯，用“费尔泼赖”又像愤青，在此我建议你用简称记录这个词。用FP。就像对应中文放屁拼音首字母。我们大家无怨无仇，今天在这里，花费这么大的社会资源聚会谈论这个神奇的案子，是在检验我们这个共同的家园的社会公平底线，法律意义上的FairPlay，而不要是中文意义上的FP。<br />
<br />
一审判决书上罗织了而十几条所谓“受害者”的证词，力图证明这个事件的社会危害严重性。我发现这些受害者的受害逻辑比较类似，不外乎：这件事影响太大了，我的个人生活深受影响，要严惩这些捏造事实的人！<br />
<br />
我认为这些证据的制作水平比较低。那些所谓的受害者，他们基本上都是公职人员。作为公职人员，因为社会上有人质疑他们的工作，就能产生那么大的困扰。甚至还有人因此调回自己老家，调回经济环境比闽清好的福清！这些人的心理素质也忒差了吧！我认为为了避嫌，也为了证明这个事情的社会影响之大、之广、之深远，应扩大受害样品采集范围。而且至少要罗织一百份证词，我愿意代劳帮马尾公安制作一个证据。<br />
<br />
下面是我拟作的一个承包公厕的吴大爷的证词：<br />
<br />
马尾公安局的陈警官找到吴大爷。我用陈，而不用涂警官，是担心马尾公安局只有一个姓涂的，会有人对号入座。福建陈姓的人多，但还是请勿对号入座。另外，吴大爷也不叫吴瑞锋，也别对号入座。<br />
<br />
问：大爷，网络上有人写文章，做视频，都是捏造事实，把我们闽清讲的一团糟，你知道吗？<br />
<br />
答：大爷我不上网，不过你告诉我，我就知道了。很受伤啊！<br />
<br />
问：那里面说闽清公安局有人与黑社会合伙开KTV，还卖毒品，还说我们闽清城关经常出现无名女尸，这些人都是被奸杀的。你说这些人可恶不可恶！<br />
<br />
答：他妈的，这么说闽清还是人呆的地方吗？要严惩这些造谣的人！<br />
<br />
问：这些文章和视频在网络上点击率很高，都有八万多，等于现在全世界都知道了，对我们闽清的形象损害大不大？<br />
<br />
答：太大了。像我这个公厕把，每天大小便坑的点击率也不过两三百。八万多，太厉害了！对我们闽清的形象损害太大了。我们闽清的治安哪有这么乱呀！自从改革开放以来，我承包公厕30年，只有一次，一个穿制服的人进来拉尿，不给钱。我拦住他，在他面前伸出两个手指头说：一次两分。他却一巴掌盖过来。说他这是在执法。我想，这世道，穿着制服，连拉尿都是执法？不过他不是穿你这制服，上面写有“工商”。我们闽清的治安不错呀，进来拉屎拉尿都正常给钱，拉屎一次五毛，拉尿一次两毛，很有规矩的。欢迎海西内外宾客前来投资，消费！要严惩造谣者，否则我的生意会受影响呀！<br />
<br />
我觉得加一些这样的证词，对说明这个事件的社会影响力，会更有说服力，希望二审法庭给予考虑。<br />
<br />
我觉得在福州的法庭上说理很困难，最后我想用一个寓言故事结束我的二审陈词：<br />
<br />
伊索寓言里有个狼和羊的故事。<br />
<br />
狼为了能在Fairplay下依法吃羊，对溪边的小羊说：“你涉嫌诽谤，所以我要吃掉你。”<br />
<br />
小羊说：“操你大爷，公安部09年4月份刚有个文件，诽谤罪可不能乱用的。那基本上是自诉案件啊！”<br />
<br />
狼说：“那我就用诬告陷害的罪名吃你，这是公诉案件！”<br />
<br />
小羊说：“我没有捏造，也没有陷害的意图呀！”<br />
<br />
于是在第三次开庭时，狼说：“你虽然没有陷害意图，但好像有捏造事实，而且这事影响这么大，算了，我就用诽谤的名义吃掉你吧。这罪名轻些，算是对你的灵魂有个交代，对我的良心也有个交代！”<br />
<br />
被吃掉的小羊，她的灵魂飘在空中，还在困惑着：既然说我没有陷害的意图，怎么就会又说我有损坏他人人格的意图呢？这狼是在Fairplay，还是在FP呢？<br />
<br />
 <br />
<br />
<br />
游精佑2010-6-13<br />
<br />
 <br />
<br />
<br />
以上是我提交的书面陈述。在法庭上因为连续开庭到将近13点，交通管制时间马上到了，审判长一再催促上诉人和辩护律师简短发言，如果有书面资料，只需提交书面资料。我最后的口头发言根据记忆整理如下：<br />
<br />
连续开庭这么长时间，大家都很辛苦。我本来想跟大家讲个故事，放松一下。也不说了，在此只提两件事。<br />
<br />
1、关于自诉和公诉。公诉人说网络诽谤是新型犯罪，因为取证困难，所以必须公诉追究。我想问一下，取证困难是不是区别公诉和自诉的充分条件？公安部关于诽谤罪公诉自诉区分的文件是09年4月份才发布的，他们不知道网络诽谤的特点吗？他们并未把它列入需要公诉解决的特殊情况之一。公安部编写这个文件的人难道是白痴？<br />
<br />
2、关于真相。一审判决和二审公诉人都有一个假定，我游精佑是明知真相的人，成千上万的网友都是不明真相的人。我要声明，我游精佑并不比网友们聪明，把他们都看成傻瓜很有意思吗？另外，政府的公告或新闻发布会的内容，就是真相，就是唯一的真相吗？一个公民听到有关内容，是不是就一定要接受它们？就变成明知真相的人了呢？他没有权利质疑或者不接受吗？<br />
<br />
刚才我提出本次庭审只让控方证人出庭，不允许我方证人出庭，我认为是不公平的。但是，我愿意把它认为是法庭实际操作中的一个失误，而不是故意为之。我是搞工程技术的，我们工程实践中分内业和外业两个部分。外业总是会有缺憾，但内业却可以做到尽善尽美。所以审判长，你问我对法庭有什么要求，我的要求只有一个：公平！希望二审法庭在本次庭审的内业阶段能体现这两个字：公平。<br />
<br />
