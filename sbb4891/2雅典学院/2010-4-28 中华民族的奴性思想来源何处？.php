													<h2>中华民族的奴性思想来源何处？</h2>
						
						
																											<div id="postmessage_587940" class="t_msgfont">中华民族的奴性思想来源何处？<br />
作者：莱茵 已被分享11次 评论(0) 复制链接 <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;中国历史上曾经有三个重要人物出场。说他们重要，因为以他们为代表的社会现实基本影响了后来历史的进程。第一个是姜子牙。姜太公钓鱼，愿者上钩。姜子牙用无钩之杆，从一介平民迅速登上历史的政治舞台，建立了一番丰功伟业，在那个混乱、动荡、贫穷的年代，点燃了无数人的梦想。姜子牙是成功的将计谋从战争中运用到政治的典范。姜子牙和后来的武王也是君臣合作成功的典范。后来的诸子百家其实也是通过著书立说来展现自己的政治主张。期望像姜子牙一样走终南捷径，得帝王的赏识，建立一番功业。第二个是赵高。赵高用他的指鹿为马计谋，篡夺了秦王朝的政权。开了一个最大的也最坏的先例。破坏了君臣关系。从此以后的君主不再相信功臣了。第三个是刘邦。刘邦的“鸟尽弓藏，兔死狗烹”的对待功臣的做法，在赵高基础上彻底破坏了君臣关系，开了一个更坏的先例。从此以后的功臣也不相信君主了。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;后来的中国封建社会彻底进入了缺乏信任关系的计谋时代。上面不相信下面。下面也不敢相信上面。在这种诚惶诚恐的强大的专制封建权力为背景的社会处境下，如何生存下去？只有一个办法，就是利用人性的弱点，猜测上面的想法，投其所好。于是上层统治者喜欢下面的投其喜好，下面的也不择手段的去追求这种投其喜好，最终于产生了对中华文化影响甚大的奴性思想。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;儒学为封建社会对人和封建等级的划分提供了思想来源，但不是奴性的来源。因为思想虽然可以塑造人，但真正使人民屈服这种塑造的却是强大的封建国家权力。等级制度在古代众多国家都存在，许多的国家等级制度的严格程度比古代中国有过之无不及，这是为了维护统治秩序稳定的需要。比如古印度就有严格的种姓制度。在欧洲有血统制度。不同的等级都在维护自己相对的尊严。但是中国的封建等级制度和他们有很大不同，它不是建立在对神和教的崇拜基础上的。可以说古代中国人的骨子里是根本不相信神的。帝王将相，鄢有种乎？他们更相信的是一种功利。一种成王败寇的功利。因为功利主义生存哲学，在中国封建社会，人与人的关系是建立在极度缺乏信任的基础上的。因为缺乏信任，所以每个人都对自己负责或对自己有价值的家族负责，形成了一个民族自私的心理性格。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;本来功利思想是对等级制度有天然的破坏力的，因为许多人都想从下个等级跳到上个等级上，形成内在的不稳定性。但是功利思想在现实的追求过程中的结果却是功利主义通过等级制度的完美结合，产生了奴性思想。正是奴性思想赋予了封建等级制度的内在生命力。结果却是封建统治的更加稳定。那么奴性是如何赋予制度以生命力的呢？<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;首先奴性的产生离不开封建的专制权力。专制的权力压迫下不一定产生奴性。有的专制和暴力会直接引起被压迫者的反抗。有时候沉默也是一种反抗，那是一直逼不得已的顺从。但有的被压迫者却会主动顺应追求这种权力体制，靠出卖自己的尊严和别人的利益，牺牲自己的社会责任和民族责任。去谋取最大的私利。所以奴性不是简单的统治者驯服，还是被统治者主动追求的一种行为。这种主动追求行为不是简单的一般行为。上面喜欢什么就要送到什么。上面有什么威胁要帮助解决。上面讨厌的要帮助排除。奴性不仅仅是一种性格，更是一种有计划，有预谋的行为。奴性的背后是切实、切身的利益关系。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;封建主导文化真正应该是计谋。计谋一开始是用在战争上的，后来用到了政治上。最后用到了社会上。从上到下，无孔不入。所谓的上有政策下有对策，道高一尺魔高一丈都是计谋与计谋的对垒。以计谋为中心的主导文化最大特征是：它不鼓励，也不通过研究，去告诉人们如何去创造更多的社会物质财富，而是告诉人们如何去掠夺、占有现有的社会物质财富。而且还为这种占有和维护提供了丰富多彩急功近利的手段。这也是封建主导文化能有强大生命力的根本原因。因为它所追求的东西和所提供的手段从最大程度上符合了人类急功近利和贪婪的本性。 奴，本身就是一种计谋。一种生存的计谋。他不但要揣测主子的真实想法。还要投其所好。而目的是想得到自己想要的利益。奴其实也是一种生存状态。生存方式。一种对权力的膜拜和操纵引起的众多人利益的集合。而由此形成的生存模式伤害的却是人民整体和国家的利益。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;许多人认为奴性的来源产生儒家思想。其实是因为在历史的文化和历史的现实，理论和实践之中我们一直存在思维的误区。儒家谈论仁义，那么以后讲仁义的人都是儒家的门生吗？难道在孔子之前就没有人讲仁义吗？孔子说：有朋自远方来，不亦乐呼！那么后人的热情好客都是在遵循孔子的教诲吗？孔子的学说其实也是对现实客观存在的事物赋予主观取舍的一种归纳，总结和探索。既然都是来源于客观世界。所以说优秀的思想是无国界的。优秀的精神是相通的。把对客观道理的崇拜转变为对人的和学说的崇拜，是我们思维方式上的本末倒置。也是一种奴性思维习惯。任何理论的价值只有通过现实才能表现出来。所以理论的价值，就是用来学习并改造我们心灵和精神世界，塑造完整的自我。用来通过改变我们的行为来改变客观世界。如果不能做到这一点，再优秀的理论也只能是一堆无聊的符号。所以对于所谓的儒家认识。我们不但要观其言，更要观其行。只有言行一致，才是真正的儒家。否则儒学只是一层虚伪的华丽外衣。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;近代的公民意识应该起源于资产阶级革命之后。公民意识是与国家权力意识相对应的。主权在民的基础必定是人与人之间的独立和平等。而资本主义的法治和三权分立制度只是实现国家权力和公民意识相互关系的一种形式。法治可以合理界定维护权力的分配，限制权力的滥用。法治的运行环境必须有与之相适应的公民意识，而不是对权力绝对服从的奴性意识。所以中国要想走向法治社会。必须要有与之相适应的公民意识。从人民走向公民。从平等自由走向民主法治，这是中华民族和中华民族内每一个向往自由平等的人性摆脱封建思想和奴性思想影响的唯一之路。</div>

							
							
							
							
