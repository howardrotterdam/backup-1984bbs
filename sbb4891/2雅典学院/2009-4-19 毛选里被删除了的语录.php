毛：“美国在日本的广岛和长崎投了两个原子弹，损害了日本人民。因此使美国在世界大部分人民中间的名声不好。……第二次世界大战中，日本政府是强迫日本人民进行侵略战争的，但后来起了变化，遭到了美国的原子弹之害，所以日本人民，包括某些政府人士也反对战争。”（同参加在日本召开的第十一届禁止原子弹氢弹世界大会后访华的外宾谈话1964年8月22日）<br />
<br />
“（抗战胜利后国共和谈期间提出）‘和平民主新阶段’是为了争取时间，准备夺 取政 权。日本投降早了一点，再有一年我们就会准备得更好一些。”１９５９.８.１７毛泽 东的一次讲话，《学习资料（１９５７～１９６１）》（清华大学，１９６７）第260页。<br />
<br />
“要冷静，不要到前线去充当抗日英雄，要避开与日本的正面冲突，绕到日军后方去打游击，要想办法扩充八路军、建立抗日游击根据地，要千方百计地积蓄和壮大我党的武装力量。对政府方面催促的开赴前线的命令，要以各种借口予以推拖，只有在日军大大杀伤国军之后，我们才能坐收抗日成果，去夺取国民党的政权。我们中国**人一定要趁着国民党与日本人拼命撕杀的天赐良机，一定要趁着日本占领中国的大好时机全力壮大，发展自己，一定要抗日胜利后，打败精疲力尽的国民党，拿下整个中国。&quot;<br />
<br />
毛泽东在一九三七年八月在陕北洛川会议上的讲话摘要<br />
<br />
&quot;有的人认为我们应该多抗日，才爱国，但那爱的是蒋介石的国，我们中国**人的祖国是全世界**人共同的祖国即苏维埃（苏联）。我们**人的方针是，要让日本军队多占地，形成蒋、日、我，三国志，这样的形势对我们才有利，最糟糕的情况不过是日本人占领了全中国，到时候我也还可以借助苏联的力量打回来嘛！&quot;<br />
<br />
毛泽东在一九三七年八月在陕北洛川会议上的讲话摘要<br />
<br />
&quot;为了发展壮大我党的武装力量，在战后夺取全国政权。我们党必须严格遵循的方针是&quot;一分抗日，二分敷衍，七分发展，十分宣传&quot;。任何人，任何组织都不得违背这个总体方针。&quot;!<br />
<br />
毛泽东在一九三七年八月在陕北洛川会议上的讲话摘要<br />
<br />
“（抗战时）一些同志认为日本占地越少越好。后来才统一认识∶让日本多占地，才爱国。否则变成爱蒋介石的国了。”《庐山会议实录》（河南人民出版社，１９９６）第182页。<br />
<br />
国民经济的两个拳头，一个屁股。基础工业是一个拳头，国防工业是一个拳头，农业是屁股┅┅稳产高产是相对的，去年河北大雨是老天爷下的，没有办法。天老爷真难当，下多了不是，下少了也不是。【在计委领导小组汇报时的一些插话1964.5.11】<br />
　　　<br />
屁有香臭，不能说苏联的屁都是香的。现在人家说臭，我们也跟着说臭。凡是适用的都要学，资本主义好的也应该学。【在中央**扩大会议上的讲话（一九五六年四月），《毛泽东思想万岁》1969年8月版，第三十七页。】<br />
<br />
事前要有准备，小会他神气大，大会他没办法。你要大民主，我就照你的办，有屁让他放，不放对我不利，放出来大家鉴别香臭。【在省、市委书记会议上的插话－－汇集（1957年1月），《...思想万岁》一九六九年八月版，第75页。】<br />
<br />
上边放的屁不全是香的，这里也有对立，有香也有臭，一定要嗅一嗅。【在省、市委书记会议上的插话（1957年1月）【...思想万岁】一九六九年八月版）<br />
<br />
人同自然界作斗争，也有交换。如人吃东西，吸空气，但要拉屎拉尿，新陈代谢。┅┅大鱼吃小鱼，小鱼吃大鱼的屎。重工业各部门之间也要等价换，远陆造机器要原料，就是粮食，机器就是他拉的屎。【在郑州会议上的讲话（1959年3月5日），《毛泽东思想万岁》1967年版，第42页。】<br />
　 　　<br />
同志们，自己的责任都要分析一下，有屎拉出来，有屁放出来，肚子就舒服了。【在庐山会议上的讲话（1959年7月23日），《毛泽东思想万岁》一九六九年八月版，第305页。】<br />
<br />
一九五八年<br />
“粮食多了怎么办？国家不要，谁也不要，农业社员们自己多吃嘛！一天吃五顿也行嘛！”<br />
<br />
“现在看来搞十几亿人口也不要紧，把地球上的人通通集中到中国来粮食也够用！将来我们要搞地球委员会，搞地球统一计划，哪里缺粮，我们就送给他！”<br />
<br />
“大概十年左右，可能产品非常丰富，道德非常高尚，我们就可以从吃饭、穿衣、住房子上实行**主义，城市乡村一律叫公社，不叫工厂，大学、街道都办公社，如鞍钢叫鞍山公社，不叫工厂。”<br />
“大城市要分散，乡村就是小城市，每个公社都将路修宽一点，可以落飞机，每个省都搞一二百架飞机，每个公社平均两架，大省自己搞飞机工厂。”<br />
<br />
“过去不少的人认为工业高不可攀，神密得很，有很大的迷信。我也不懂工业，对工业也是一窍不通，可是我不相信工业就是高不可攀。我和几个管工业的谈过，开始不懂，过几年，也就懂了，有什么了不起！”<br />
<br />
“赶超英国，不是十五年，也不是七年，只需两到三年，两年是可能的！”<br />
“为五年接近美国，七年超过美国这个目标而奋斗吧！”<br />
“七亿人口搞七亿吨钢。三至七年之内建成一个工业大国。”<br />
“民法和刑法那一类法律都不需要了。民法刑法那样多条谁记得了？一搞大跃进，就没时间犯法了。”<br />
法律这东西没有也不行，但我们有我们这一套，还是马青天那一套好。……不能靠法律治理多数人，多数人要靠养成习惯。军队靠军法治人，治不了，实际是 1400人的大会 (指1958年的军委扩大会) 治了人，民法刑法那样多条谁记得了。宪法是我参加制定的，我也记不得。韩非子是讲法治的，后来儒家是讲人治的。我们每个决议案都是法，开会也是法。治安条例也靠成了习惯才能遵守，成为社会舆论，都自觉了，就可以到**主义了。我们各种规章制度，大多数，百分之九十是司局搞的，我们基本不靠那些，主要靠决议，开会，一年搞四次，不靠民法刑法来维持秩序。人民代表大会，国务院开会有他们那一套，我们还是靠我们那一套。<br />
<br />
1958年8月21日下午毛泽东在北戴河会议上<br />
<br />
・・・・・・开后门来的也有好人，从前门来的也有坏人。・・・・・・批林批孔又夹着走后门，有可能冲淡批林批孔。小谢、迟群讲话有缺点，不宜向下发，我的意见如此。<br />
1974年2月15日，毛泽东在给叶剑英回复信中语<br />
<br />
我是个粗人，很不文明。有人说，中国爱好和平，那是吹牛，其实中国就是好斗，我就是个。<br />
（召见首都红代会负责人的谈话　１９６８、７、２８）<br />
<br />
现在的考试办法是对付敌人的办法，而不是对人民的办法。实行突然袭击，出偏题，出古怪题，还是考八股文章的办法，我不赞成，要彻底改革。我主张公开出考题，……考试可以交头接耳，甚至冒名顶替。冒名顶替的也不过是照人家的抄一遍，我不会，你写了，我抄一遍，也可以有些心得。可以试点，要搞得活一些，不要搞得太死。先生讲课有的罗罗索索，允许学生打磕睡，你讲得不好，还一定让人听，与其睁着眼睛听着没有味道，还不如睡觉，可以养养精神，可以不听，稀稀拉拉，休息一下脑筋。【召见首都红代会负责人的谈话（1968年7月28日）】<br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
做为一个屁民俺们吃够了被删除和被屏蔽的苦.一度很想不开.<br />
<br />
后来看到毛伟人也被删除和被屏蔽.俺就想开了.俺现在情绪特稳定</div>

							
							
							
							
