													<h2>古代中国是个传统的专制国家吗</h2>
						
						
																											<div id="postmessage_790207" class="t_msgfont">古代中国是个传统的专制国家吗<br />
<br />
2010-08-18 天涯煮酒论史 作者：耶律阿保机<br />
<br />
　　现在当人们谈到古代中国的政治制度时，首先想到的通常都是君权专制，中国是个传统的君主专制国家云云。这已经成了人们的定式思维。其实仔细考察起来，这里面有一个很大的误区。<br />
<br />
　　什么是专制？一般对专制的定义以孟德斯鸠在《论法的精神》中提到三种政体时的定义为较准确。即“专制政体是既无法律又无规章，又单独一个人按照一己的意志与反复无常的性情领导一切。”<br />
<br />
　　认为中国古代为专制的依据通常有两个，一是君主皇位是世袭的；二是君权至高无上，没有任何力量能有效制约。<br />
<br />
　　对前一条，我们会认识到那不是专制的必要条件（更非充分条件）。因为君主立宪的政体中王位也是世袭的。反过来如过没有世袭制度，并不意味着就一定不会造成“单独一个人按照一己的意志与反复无常的性情领导一切”的情况。因此我们重点需要考察的是第二条。<br />
<br />
　　中国古代，真的君权至高无上，没有任何力量能有效制约吗？可能未必。<br />
<br />
　　先举几个例子。摘录钱穆先生《中国历代政治得失》中的一段话。<br />
<br />
　　“如唐制，中书舍人拟稿，亦由诸舍人各自拟撰，是谓“五花判事”。然后再由中书令或中书侍郎就此许多初稿中选定一稿，或加补充修润，成为正式诏书，然后再呈送皇帝画一敕字。经画敕后，即成为皇帝的命令，然后行达门下省。所以唐代政府定旨出命之权，是操于中书省。皇帝只同意画敕而止。待门下省主管长官侍中及副长官侍郎接获此项诏书后，即加予复核，这是对此项命令之再审查。在门下省侍中侍郎之下，设有若干第三级官，谓之“给事中”。给事中官位并不高，但对皇帝诏书亦得参加意见。若门下省反对此项诏书，即将原诏书批注送还，称为“涂归”。意即将原诏书涂改后送还中书省重拟之意。涂归亦称“封驳”“封还”“驳还”等，其意义略相同。此项涂归封驳之权则属诸门下省。若以今日惯语说之，门下省所掌是一种副署权。每一命令，必须门下省副署，始得发生正式效能。如门下省不同意副署，中书命令便不得行下。诏敕自中书定旨门下复审手续完成后，即送尚书省执行。尚书省则仅有执行命令之权，而于决定命令则无权过问。”<br />
<br />
　　丛这段材料看，作为中国古代朝代典范和代表的唐朝，真的是君权专制吗？凡属皇帝命令，在敕字之下，须加盖“中书门下之印”，即须政事堂会议正式通过，然后再送尚书省执行。若未加盖“中书门下之印”，而由皇帝直接发出的命令，在当时是认为违法的，不能为下面各级机关所承认。到了武则天的时候，她想违背这些规定，却遭到了一个大臣刘t的批评，“不经凤阁鸾台何为敕！”（中书省武则天改称凤阁，门下省武则天改称鸾台）。刘t能如此理直气壮地说出这种话，是一个习惯于皇帝凭个人意志领导一切的政府中的官员说得出来的吗？<br />
<br />
　　这样的例子，正史中还有很多，比如宋代赵普有名的“法者天下之法也，非陛下一人之法”的论断。还有钱穆先生提到的宋朝的例子：“建德二年，恰逢三个宰相相继去职，太祖欲派赵普为宰相，但皇帝诏敕一定要经宰相副署，此刻旧宰相既已全体去职，一时找不到副署人，该项敕旨，即无法行下。宋太祖乃召集群臣会商办法，当时有人献议说：‘唐代皇帝曾有一次下敕未经宰相副署，此在甘露事变时，当时前宰相已死，皇帝临时封派宰相，即由尚书仆射参知政事者盖印，今可仿此方式办理’，同时即有人反对，谓‘唐代甘露事变，虽曾用此方式，但为乱时变通权宜办法。今大宋升平，不应采此方式。’如是再四商讨，是决定由当时开封府尹副署盖印行下。”<br />
<br />
　　这些活生生的例子中，能得出君权至高无上，没有任何力量能有效制约的结论吗？<br />
<br />
　　或许会有人说这是特例，不足为训。主流仍然是君权专制。而值得注意的是，这些例子都是写进官方正史，作为正面材料以助后世“资治通鉴”的。到了这种程度，难道还能眼睛一闭，用“特例”二字搪塞过去吗？<br />
<br />
　　到这里，也有人会问：“既然皇帝的权力受到那么大限制，那后来宋代徽宗朝何来御笔断案，引例破法之事？”<br />
<br />
　　这个问题不妨这么问：“如果皇帝的权力真的毫无限制，那为什么宋徽宗不直接修改《宋刑统》而要用御笔手诏来引例破法呢？”<br />
<br />
　　由此可见，中国古代的君权，不是没有制约的。相反，它受到相当大的制约。这些制约与我们平时所说的法律的强制执行力不同。这些制约就是“成宪”，俗称“祖宗之法”。这些规范部分来自古代社会千百年的乡土道德，部分则来自各朝开国帝王的开创。这些成宪规定了后代君主行为自由的程度和范围。它效力的来源是一个高度崇拜祖先的农业民族的传统舆论。<br />
<br />
　　有人会很自然地想到，这些习惯根本没有明文规定，更谈不上系统的宪法。它的效力完全寄托在君主的自觉遵守上，这是非常不可靠的。<br />
<br />
　　这种想法实际上过分强调了制度建构的形式要求，而忽视其实质内涵。一套为人们广泛遵行并真诚信仰的行为规范，其效力是最高的――即使可能唯一保证其实施的力量就是舆论。但是永远不该无视舆论的力量。所谓千夫所指，无疾而死。尤其是受到多年广泛认同的那种舆论。其威力绝不可轻视。事实上，无论多么完备的制度，归根结底其保证实施的依靠都是人们的自觉遵守。<br />
<br />
　　13世纪英国大宪章被许多人视为君主立宪开端标志而被津津乐道。甚至有更滑稽的说法说英国自此就有了宪政。不错，大宪章以白纸黑字签名画押的形式，对君主的权力做了明确的限制。当时的英国国王是无地王约翰，他亲自签署了大宪章。但鲜为人知的是，终其一生，约翰从没有遵守过这份文件。为此君臣之间大打出手，内战一直持续到约翰去世还没有结束。比起中国皇帝们面对的成宪祖制，大宪章又是怎样一种效力呢？<br />
<br />
　　罗素在《权力论：新社会分析》中提到一种舆论的建立通常需要三个阶段：一，少部分人接受，并开始向其他人宣传；二，相当一部分人接受，并有条件用强力保证其得到宣传和遵行；三，舆论已成为人们心目中真诚的信仰，强力只起到轻微的辅助作用甚至已经成为多余。那些认为中国古代是专制国家，其统治原则是恐怖，君主与臣民之间不会有真正的爱之类的观点（均出自《论法的精神》），其实都只是错误地相信中国古代皇帝的权威只停留在上述的第二阶段。而实际上很多时候早已进入第三阶段的凝固期。这种凝固后的力量之大，足以将皇帝本人也陷入其中难以挣脱。<br />
<br />
　　但是，会不会挣脱呢？必须负责任地承认，会的。如果说中国古代没有出现过专制君主，那不符合事实。但也必须指出，只有那些精力充沛，雄心勃勃同时个性最鲜明，最离经叛道的君王，才有条件冲破强大传统舆论的束缚，追求孟德斯鸠所描述的专制状态。清朝以前（清朝有特殊情况，下文详述）能算得上专制君主的可能只有秦始皇嬴政、汉武帝刘彻、隋炀帝杨广、唐代女皇武则天、明太祖朱元璋、明武宗朱厚照这几个。而其中真正能够达到专制的基本要求的恐怕只有嬴政和朱元璋这两个。只有他们能做到靠一个人的意志治理“整个”国家。<br />
<br />
　　从事实情况上来说，在幅员辽阔，疆域如此宽广，人口如此众多的古中华帝国，特别是在技术不发达的古代，实现个人专制实际上可能性极小。因为一旦如此就要求专制者大得可怕的工作量。似乎只有上述秦始皇和明太祖敢于进行这种伟大而危险的尝试。嬴政能够坚持每天批阅几十斤写在竹简上的奏章。而胡蓝党狱之后，朱元璋废除宰相制度，从此他不得不接受这个行为的副产品：每天多得吓人的事务。据说十几天内就会从全国各地送来数千份呈请皇帝批阅的奏章，其中的绝大多数一份奏章说的还不止一件事――所以说要成为专制君主，精力充沛和足够的才能必不可少。到了秦始皇和明太祖的不肯能有如此超人精力才能的后代，他们无法坚持这种高强度超负荷工作量，只好降旗认输，找身边的人分流掉一些。于是秦二世有赵高专权。而明朝则更有戏剧性。宰相没有了，却有了有其名无其实的内阁大学士。任用太监帮忙批阅奏章竹简的权益也逐渐制度化，乃至产生了专门从小培养秉笔太监的学校。这两者差不多就这样代替了宰相的职能。加上长时间在文官系统中凝固起来的“祖制”和“成宪”。让本来一度成为中国有史以来最集权朝代之一的明朝，反而出现了接近君主立宪般有力的君权制约。正如黄仁宇在《中国大历史》中所说的那样（虽然黄仁宇先生本人将古代中国视为专制），尽管他认为“当一个国家尚不能用数目字管理时，君主立宪不可能成为事实”，可是他也承认“独裁制也有它力之所不能及的地方。当日北京的朝廷……绝不可让皇帝尽量地发展其人身的权力。虽说在理论上讲，这种权力并无限制”。<br />
<br />
　　有名的明武宗正德皇帝朱厚照，作为历史上罕见的特立独行的帝王，屡次试图冲破这些制约，干了不计其数的荒唐事，同时也受到了令他无比心烦的来自文官系统的压力。矛盾终于在1519年爆发。起因是皇帝企图到江南游玩，146个文官赴阙啼泣，哀求收回成命。，朱厚照大为震怒，他指令凡抗命不离宫门的官员每人受廷杖30下，其中11人或当场打死或以后伤重而死。明朝建立以来规模最大的文官集体处罚事件，显示了“专制”的强大力量，然而最终屈服的却是皇帝。这个明朝个性最强的皇帝终于在成宪面前低头了――虽然很有限，但还是低头了。<br />
<br />
　　正因为专制可能性很小，因此，自秦汉大帝国肇造，皇室与政府就开始分离。众多的行政职能部门产生。皇室成员不再天然是国家官员。（欧洲类似的变革在那以后1200多年才出现）。这些正说明治理一个大帝国的复杂艰难以及华夏祖先的良苦用心。而近人以“黑暗专制”四个字轻易否定了中国2000年来的政治制度；以“官僚主义”四个字彻底抹煞了古代中国行政组织学方面的成就，仿佛三公九卿、三省六部都是古人庸人自扰，设了好玩儿似的。这不是我们应该仿效的态度。<br />
<br />
　　最后谈谈清朝的特殊情况。按照钱穆先生的说法，清朝可以勉强说是中国历史上唯一专制的朝代。清朝的皇帝教育保留了少数民族重视体育锻炼的习俗。所以清朝的皇帝（特别是前中期）大都武艺高强，精力充沛，有能力进行系于一身的高度集权统治。同时清朝很大程度上延续了明朝“从理论上讲”君权无上的制度。所以，清朝产生了比较多的专制君主。特别是雍正朝设立军机处以来，皇帝的命令通过军机处越过六部直接下达，政府机关被架空。因此可以说，清朝的专制程度几乎超过此前的所有朝代。<br />
<br />
　　清朝距离我们最近，得到其资料比得到其他朝代的容易。而影视媒体又喜欢前述那些各朝的专制皇帝，因为这些皇帝都比较有个性，所以他们的奇闻轶事也比较多。有这些原因，人们接触到的关于中国古代专制君主和制度的资料很多，会产生中国古代由君主专制统治的错觉。加上孟德斯鸠在他知名度如此之高的《论法的精神》中点名论断中国是专制政体，更加深了人们的信念。但是准确地说，古代中国的确产生过一些专制皇帝，但不宜断言中国是个传统的专制国家。</div>

							
							
							
							
