有人在网站里面谈到我李敖，说你李敖有点偏心哦，你对中国的批评，凡是涉及到国民党部分，涉及台湾的部分，你就比较苛刻，比较严格，涉及到共产党部分，中国大陆的部分你就比较宽大，比较松，你是不是两面人啊？国民党关了你，你就骂它骂得狠，对共产党你就客气，是不是啊？我跟大家说，你太小看了，把我李敖看得太小了，你小心眼，你的见识啊就变小了。中国古代一位了不起的思想家，也是政治家叫做晏子，晏子有一次呢出门，有个女孩子在路上拦他，漂漂亮亮的这个女孩子啊，就要私下跟晏子讲一句话，什么话呢？她跟晏子说我有一件事情啊要拜托你，什么事情呢？说我爸爸遭遇什么困难，我希望你帮我爸爸解决这个问题，什么代价呢？我愿意陪你睡觉，许身给你陪你睡觉。这个晏子就很感慨，感慨什么呢？说我给人家什么印象，什么坏印象，居然使这个女孩子觉得她陪我睡觉，认为用这种代价，我就会帮她忙，就表示我根本品格很低嘛，我的水准很低嘛，水平很差嘛！人家用这种标准来看我。我告诉大家，用很多标准来看人的时候，不要小看了人，我李敖就国民党关了我，我就恨国民党啊？共产党没关我，我就捧共产党？太小看了人，我是告诉大家，为什么两个标准不一样，其实是一样的，就是说，我们要给他们时间，什么时间？一九四九年中国共产党抢到了中国的统治权的时候，是一穷二白，国民党把能搬走的全搬走，能炸掉的全炸掉，留给中国大陆是两百万土豪劣绅、流亡土匪要你清除，清除这个台面，国民党把整个国库九十二万两黄金搬到台湾去，请问共产党在接受这个摊子的时候，要使中国能够慢慢复原，是不是要一段时间？因为被国民党打劫得这么严重，破坏得这么严重，赶上了美国人在韩战出了问题，高丽战争出来了，又为了中国的安全，中国的尊严，一定要跟美国帝国主义打一仗，这个时候又打了这个战争的时候，中国要不要付很多代价？所以这个时候啊，我们发现连吃顿饱饭都是很难的时候，谈什么事情，先谈吃饭要紧嘛！就这样子啊，是在这个情况底下，慢慢地成长起来的，然后再经过自己的错误，从文化大革命再往前算，从大跃进开始，如邓小平所说，我们耽误了二十年，说这个时候呢，我们在摸索里面前进，好容易现在由于邓小平最后他处理的方法的正确，总算目前现在开始走这条路了，不是吗？中国不是这样走来了吗？我李敖小的时候，亲眼看过日本兵骑著马，在我们的国家里面那样子耀武扬威，所以对我这个年纪说起来，一个强大的中国对我是非常重要的，对我的信仰是非常重要的。所以我在跟大家讲，我们中国讲富国强兵，什么叫富国强兵？大家看，中国的《二十四史》，就《二十五史》里面，《史记》里面的，《孟子荀卿列传》里面的一段话，他说当时的秦国，秦用商君，就是商鞅，富国强兵，这四个字太吸引我了。我们中国太穷了，谈不到富国，可是强兵很重要，为什么呢？因为从鸦片战争以来，中国人被洋鬼子打，我们中国国力太弱了，这个洋鬼子里包括东洋鬼子，包括日本人，所以跟我说起来，我们中国有一个武力，使全世界的洋鬼子、东洋鬼子不敢打我们，这个对我李敖太重要了，对我们中国也太重要了。今天享受到这种结果的人，知道不在话下，当然没人敢打我们，可是你知道我们付了多少代价吗？我们花了一百五十年的时间，花了千万人头落地的代价，才换得了最后中国还没有富国可是先强兵的这个境界，我们付了那么多代价。<br />
<br />
通过这些比较可以看出，中华民族五千年的历史上，第一位巨人毫无疑问是毛泽东，因为他起到了其他人无法代替的作用。他诞生的时代，中华民族正处于历史上的最低点，面临生死存亡的危急关头。众所周知，当时的世界上没有谁瞧得起中国人，“东亚病夫”和“华人与狗不得入内”成为中国人深切的耻辱和隐痛。正是毛泽东使中国站起来了，朝鲜战争使全世界都对中国刮目相看，从此中国人扬眉吐气。中国成为举世公认的世界军事强国。<br />
<br />
但中国成为世界军事强国，并不等于世界经济强国。20世纪强国与弱国、富国与穷国的主要标志是工业化的程度。又是毛泽东使中国在短短二十多年内走完了西方发达国家上百年才能走完的工业化道路，成为世界主要工业强国之一。到1980年，中国的工业规模已经超过世界老牌工业强国英法两国，直逼在西方强国中坐第三把交椅的西德(见美国著名学者保罗?肯尼迪的名著《大国的兴衰》)。20世纪80年代中期，中国的工业总产值跃居世界第三位。<br />
<br />
目前许多人有一种错误认识，即认为毛泽东不懂经济，导致中国大陆的发展落后于同期的日本和“四小龙”。这是当今最大的谬误。首先，日本和“四小龙”的起点与中国不一样。日本在明治维新后就开始了工业化，到二战时，日本能够年产上万架飞机，而中国连一架飞机都不能制造。许多人以为德国和日本经过二战，变成了一片废墟，其实战时德国和日本的工厂都隐藏到地下或者分散，美国的飞机根本奈何不得，否则美国根本不需要扔原子弹和拉拢苏联出兵，用飞机将倭国工厂统统炸光就行了。手持木棍的日本兵还抵挡美国人吗?日本到1945年7月仍有月产上千架飞机的能力，比1942年的飞机月产量还高，说明其工业基本完整无损。何况日本拥有一支宏大的科技队伍，以及一支熟练技术工人大军，这是旧中国根本无法比拟的。因此到新中国成立的1949年，日本实际要领先中国80年。即使是台湾和韩国，由于它们曾经成为日本的部分领土(殖民地)，整体水平也要领先中国大陆50年(谁如果不信这一点，可以去阅读著名作家萧乾在1946年写的《冷眼看台湾》一文)。而香港和新加坡作为城市，以整体水平来衡量当然要比中国大陆(大多数地区是农村)发达得多。何况它们还是英国远东殖民地的两大中心城市，英国从殖民地掠夺的大量财富都集聚到这里，所以中国大陆与它们的出发水平不同。经过毛泽东时代的短短三十年，中国实现了从农业国到伟大工业强国的历史性跨越。中国实行的是赶超战略，即瞄准西方先进的工业水平，别人有的我们要有，别人没有的我们也要有。到毛泽东去世前夕，几乎所有西方有的中国都有了。天上有喷气式飞机，地下有汽车、火车和轮船，卫星、导弹、原子弹样样俱全。而“四小龙”能造什么？它们是典型的殖民地经济，只能造服装鞋帽之类。它们赶上了冷战时期的好时光，那时西方殖民地各国的反帝独立斗争风起云涌，对西方来说，只有在美英刺刀保护下的“四小龙”才是安全的，因此投资都集中于它们，造成了它们的短暂繁荣。但它们没有发达的制造业和核心技术，繁荣根本长不了。20世纪初期的阿根廷靠养羊也曾富得流油，但现在如何呢?冷战结束后，“四小龙” 的经济发展很快陷入困境，而中国却仍在高速前进，因为中国有自己的制造业，几乎什么都能够制造。而中国的工业化基础正是毛泽东为中国人民奠定的。<br />
<br />
<br />
诚然，毛泽东时代的中国人民生活水平提高得并不是很快，但这是为了筹集中国工业化的资金。西方工业化的资金来自殖民掠夺（英、法）、剥削黑奴（美国）、对外征战获得赔款（德、日）。而象苏联和中国这样的社会主义国家，只能首先节衣缩食积累工业资金，将自己的工业发展起来，然后利用工业创造的财富享受美好生活。在毛泽东时代，中国首先建立了大量的矿山铁路，然后是钢铁厂，再以后是机械厂。当机械工业初具规模后，就可以建立各种轻纺厂、自行车厂、电视机厂等等，以改善人民的生活。但毛泽东就在中国即将从积累时代转向消费时代的时刻离开了我们，没有看到人民生活获得极大改善那一天的到来。这是他一生的遗憾。而许多不明道理的人，却将毛泽东时代看成贫穷的时代。<br />
令人遗憾的是，最近一二十年来，中国的某些经济学家被四小龙的暂时发展所迷惑。在他们的鼓吹之下，中国一度采取了与四小龙相似的经济发展路线，即追求的是赚钱而不是赶超。这些年来，中国利用外资建立了许多低水平的加工工业，赚了一些钱，却没有集中力量进行艰苦的技术赶超，导致中国的技术水平与西方的差距越来越大。毛泽东时代，中国曾经取得了许多举世瞩目的科技成就；而最近一二十年来，中国取得了哪些引以为傲的技术成就呢？也许只有飞船上天了，但这是靠毛泽东给我们留下的技术底子。如果当初我们按照毛泽东“中国人民有志气，有能力，一定能够在不远的将来，赶上和超过世界的先进水平”这句话指引的道路走下去，中国今天已经是与美国并立甚至超过美国的现代化强国。<br />
<br />
中国人的生活水平与美国等西方国家相比，绝不是许多人所认为的天上地下般的差距。如果这些人有幸到“四小龙”实地生活体会一下，就会发现“四小龙”的绝大多数老百姓并非他们想象的那样富得流油，日子艰难的大有人在。即使是欧美发达国家，大多数老百姓生活同样是紧巴巴的，基本上在银行没有存款。欧美国家的居民通常只生一个孩子，绝不是他们有计划生育的意识，而是难以负担两个孩子的供养和教育费用。有些人认为毛泽东是专制独裁。他们不知道，西方民主制度的特点是一盘散沙，软弱无力。美国也承认，凡是实行了美式民主的发展中国家，至今未有能够在经济方面取得成功的。20世纪初期德国和日本工业化进展最快，它们的君主专制制度起了重要作用，因为强大的中央集权体制使它们能够将资金集中于重工业。战后，日本和“四小龙”在它们的高速发展时期，都不是真正的民主国家，而实际是专制国家（日本是自民党长期执政，新加坡是李光耀家族掌权，香港是殖民统治，台湾和韩国是军人独裁）。中国没有在20世纪后半期实行美式民主，是中国的大幸。可以毫不夸张地说，如果要阻止中国在21世纪成为世界首强，最好的方法就是实行欧美式民主制度。菲律宾近年光荣地实行西方式民主，选出的总统都是演员和美女。我们不难想象，如果在中国实行西方式民主，那么中国众多的小燕子迷们就会将身穿日本军旗装的赵薇抬上总统宝座。当然赵薇的军旗装事件很可能只是无心之失，但如果被扶上去是另外一位为金钱不惜匍伏在洋人胯下的女明星，由这样的人来领导伟大的中国赶超西方，简直就是天大的笑话。毛泽东给中国带来了社会主义制度。有些人认为社会主义的大锅饭不能使人们发挥积极性。其实，西方现代企业都实行所有权与经营权分离的制度，由专门的管理层经营企业，老板只占有股份。但西方拥有一套严格的奖罚制度，谁要是搞砸了企业，他将终生没有饭碗；而中国的官员搞砸了企业，还可以易地为官。可见，中国缺乏的是有效的奖罚机制，而不是所有制的问题。那些鼓吹中国应全面实行私有制的经济学家们，都应该埋名隐姓到私企去体验一下生活，亲身尝尝老板和工头们拳脚的滋味。有过这一番体验后，相信他们中有良心的人会纷纷从右派变成左派。<br />
<br />
有些人拿今天朝鲜的情况，认为毛泽东时代的经济不行。其实朝鲜是战后经济发展最快的国家之一。即使按照台湾方面的估计，20世纪80年代朝鲜的人均国民生产总值在亚洲也仅次于日本和“四小龙”，而高于所有其它实行资本主义制度的亚洲各国（不包括西亚石油国和作为资源富国的马来西亚）。目前朝鲜的经济困难，主要是其主要贸易对象苏联东欧集团解体和西方封锁的结果。我们可以想象一下：将韩国封锁几十年会是怎样的情形？也许不用几十年，只要几年就可以使其制度优越的神话彻底破灭。<br />
<br />
。。。。。。。。<br />
<br />
毛泽东时代的另一大特色，是全体中国人民具有很强的凝聚力，这是中国成为现代化强国必不可少的条件。人们看看那些近代发展最快的国家如日本、德国和苏联。德国和日本由于统治集团大肆宣传本民族优越论，客观上造成德意志、大和民族具有很强的凝聚力。苏联是用共产主义理想凝聚人心，也使其各民族具有高昂的战斗力。中国在毛泽东时代，可以说是亿万军民众志成城，世界上没有任何国家敢惹。可以断言，如果毛泽东在世，给美国天大的胆子也不敢炸中国大使馆。令人遗憾的是，毛泽东逝世以后，中国某些不明道理的知识分子，大肆攻击毛泽东、共产党和社会主义制度，而推崇西方的所谓民主(他们忘却了孙中山的教训)，造成社会人心混乱。他们并不真正了解欧美和“四小龙”，盲目推崇西方，使中国人在一夜之间丧失了民族自豪感和凝聚力，众多的人才外流。现在许多有点钱或技术的人都想移民西方，许多有点姿色的女人也都想嫁西方人（哪怕是白发老翁）。本来香港和台湾根本不值得我们效仿，它们近年的萧条已经说明了其殖民地经济模式的失败。但在这些知识分子们的美化下，香港和台湾被许多人看成了天堂，那些没有文化、没有才干，只会吼几嗓子的港台歌星们，竟然成了内地众多少男少女倾慕的对象。在毛泽东时代，中国的少年儿童们高唱着气壮山河的革命战歌，这样有战斗力的一代没有任何国家不害怕；而现在中国的少年儿童们沉迷的是港台的靡靡之音，哈美哈日哈韩。这样，美国会把中国放在眼里吗?<br />
