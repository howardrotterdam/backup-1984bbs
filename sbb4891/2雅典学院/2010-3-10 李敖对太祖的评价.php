&nbsp; &nbsp; 大家看我这个李敖有话说，电视节目，一方面看到我上天下地地这样讲话，一方面看到我很多的见仁见智，甚至是我的偏见，来向大家来表达。可是，把我的这种偏见像筛子一样把它过滤掉，把它筛掉，剩下的是什么？剩下的是资料，我真实的知识，这也算是我这节目里面的一个功德。你可以不听我的意见，可是呢，你无法否决我的资料，而我的资料呢，在这方面就好像一个演艺人员一样，用很多的道具来表达我的思想。道具啊，我信手拈来就是道具，譬如说，大家看到我手里拿了一片我们叫做面包，可是有人到店里去买，说买土司，这个是不是土司啊？这个不是土司，我们说要土司，英文叫Toast 是错的，为什么呢？是烤了的面包，烤了以后才叫土司，所以说我们买土司，错的，你们没有烤它，不应该叫土司。Toast英文有两个意思，一个是我向你干杯，我向你敬酒，一方面呢就是烤面包。大家看，这个切开了以后呢，很薄，叫薄片的面包，这个厚，厚片面包，所以一般我们在台湾店里就说，这就是薄片土司、厚片土司。<br />
<br />
&nbsp; &nbsp; 我跟大家讲过，讲土司是错的，为什么谈到薄片厚片？大家看到没有，我手里拿着这个东西，这个是老式的唱片，注意这个老式唱片，我们叫黑胶唱片，现在人用的是越来越少，当年就是黑胶唱片，留声机用的是黑胶照片。大家看看，我手里拿的这一张也是黑胶照片，请注意啊，请看厚度，大家注意这有两个厚度，一个薄，一个厚，这个厚的厚好多，厚三倍四倍，一个就是薄。这是七十八转的唱片，为什么一个薄一个厚？大家看到我刚才拿的面包，就是作为一个伏笔，大家看，面包有薄的有厚的，怎么搞得这个唱片也有薄的厚的？请看这个薄的 Victor，当年很有名的唱片，可看看那个厚的，Edison爱迪生，爱迪生发明这种美国式的留声机，这个大发明家爱迪生的唱片。<br />
<br />
&nbsp; &nbsp; 大家记不记得，我在我的节目里面给大家看过类似这种照片，就是爱迪生当年发明这个留声机的时候，它这个唱盘是圆的，我曾经在节目里面给大家看过这种圆唱片，圆的。打开看，里面是个什么？是个圆筒形的，拿出来看，圆筒形的，套上去，就套到这里，然后它在转，然后声音就出来了。当时爱迪生发明了这种圆筒形的唱机以后，留声机以后，大家都感谢他，都赞美他，可是这种圆筒形的唱片被另外一种黑胶形的唱片给打败了，就是人家发现啊，这圆筒形的唱片来换的时候啊，收藏的时候，不方便，而这个换片的时候，收藏的时候，还有正反的效果，比较方便。这爱迪生啊就抵抗，他虽然发明了留声机，可是他抵抗这种改良型的唱片，他希望还是这种唱片，抵抗，最后抵抗不住了，他就搞出来这种唱片，跟别人一样，可是它比别人厚，厚了这么多，厚还不严重，它这种东西啊，是跟着机器走，只有它的机器，这种型号的机器，才能用这个相片，另外一种型号的机器，就用另外一种。可是别人的唱片呢，就是不但比它薄，并且适合任何型号的机器，任何型号的机器装上去，就哇哩哇啦可以唱出来，所以呢他又被打败了。<br />
<br />
&nbsp; &nbsp; 我讲这个给大家听，要证明什么？证明你是前卫人物，你是先行者，可是你有你的限度，你有你前卫的限度，先行的限度，到了极限以后，如果你不肯功成身退，你可能就是你所提倡的这个理想，或者你所发明的这个科技，你所发明的留声机，你自己就是这种发明的一个阻力，你就变成了你所发明东西的拦路老虎，你自己落伍跟不上时代，就阻碍了你自己的发明。我们再把这个意思扩大地说，就是当你是个革命先行者，当你是一个人类的先知，当你跟不上时代的时候，当时代跑得比你快的时候，你想做砥柱于中流，挽狂澜于既倒，你会失败，并且整个的失败过程会很难看，为什么呢？你变成你自己的拦路虎。最有名的一个例子就是康有为，大家看我写那本小说《北京法源寺》，整个在写这个故事，就是说你本来是时代的先行者，时代的先驱，时代的先知，可是当你带着群众这样走的时候啊，群众跑得比你快，你由先知变成了一个落伍者，你当然情何以堪，你当然心有不甘。可是怎么样呢？你会挣扎，你会用厚片土司来挣扎，可是没有用，现实是无情地把你打倒，可是如果你及时功成身退、激流勇退、金盆洗手、提早收山，就不会有这种现象，不是吗？<br />
<br />
&nbsp; &nbsp; 所以，从小可以喻大，我们看到人类很多事情，看到了很多革命的先行者，在革命成功了以后，他要怎么面对。我跟大家讲过，我们中国在一九四九年以后，就发生了这种现象。一九四九年以后，当那个反革命的蒋介石兵败山倒，赶到了台湾去的时候，那些伟大的革命者在中国的大陆，他们就面对了一个问题，我们现在要建设一个新的中国，新的中国站起来了，我们要建设新的中国。可是这些搞革命有一手的专家，他们对建国不一定有一手，有的时候他们会跟不上，所以呢，就正如毛泽东所说的，他们也在摸索，并不是一开始我们觉得什么都会，不是的，我们也在摸索，在摸索的过程里面，就会走错路，走错路就会付代价，这就是邓小平的最后的统计。在《邓小平文选》里面，我给大家展示过几次邓小平的统计，就是从大跃进到文化大革命，前后浪费了二十年，换句话说呢，有二十年的时间是走了错路，二十年时间走错路，我们付了太大的代价。这就是说，我们虽然是革命的先行者，我们虽然是发明了留声机的人，可是当留声机不断地进步的时候，它的唱片在进步的时候，要由滚筒式变成黑胶式，黑胶式还要变成很薄片的，像面包一样，有薄片厚片之别的时候，这个时候你要转过来，要跟着上，还要迎头赶上，还要超越前进，这个时候就很费力了，这就是整个的一个情况。<br />
<br />
&nbsp; &nbsp; <strong>有人在网站里面谈到我李敖，说你李敖有点偏心哦，你对中国的批评，凡是涉及到国民党部分，涉及台湾的部分，你就比较苛刻，比较严格，涉及到共产党部分，中国大陆的部分你就比较宽大，比较松，你是不是两面人啊？国民党关了你，你就骂它骂得狠，对共产党你就客气，是不是啊？我跟大家说，你太小看了，把我李敖看得太小了，你小心眼，你的见识啊就变小了。中国古代一位了不起的思想家，也是政治家叫做晏子，晏子有一次呢出门，有个女孩子在路上拦他，漂漂亮亮的这个女孩子啊，就要私下跟晏子讲一句话，什么话呢？她跟晏子说我有一件事情啊要拜托你，什么事情呢？说我爸爸遭遇什么困难，我希望你帮我爸爸解决这个问题，什么代价呢？我愿意陪你睡觉，许身给你陪你睡觉。这个晏子就很感慨，感慨什么呢？说我给人家什么印象，什么坏印象，居然使这个女孩子觉得她陪我睡觉，认为用这种代价，我就会帮她忙，就表示我根本品格很低嘛，我的水准很低嘛，水平很差嘛！人家用这种标准来看我。<br />
<br />
&nbsp; &nbsp; 我告诉大家，用很多标准来看人的时候，不要小看了人，我李敖就国民党关了我，我就恨国民党啊？共产党没关我，我就捧共产党？太小看了人，我是告诉大家，为什么两个标准不一样，其实是一样的，就是说，我们要给他们时间，什么时间？一九四九年中国共产党抢到了中国的统治权的时候，是一穷二白，国民党把能搬走的全搬走，能炸掉的全炸掉，留给中国大陆是两百万土豪劣绅、流亡土匪要你清除，清除这个台面，国民党把整个国库九十二万两黄金搬到台湾去，请问共产党在接受这个摊子的时候，要使中国能够慢慢复原，是不是要一段时间？因为被国民党打劫得这么严重，破坏得这么严重，赶上了美国人在韩战出了问题，高丽战争出来了，又为了中国的安全，中国的尊严，一定要跟美国帝国主义打一仗，这个时候又打了这个战争的时候，中国要不要付很多代价？所以这个时候啊，我们发现连吃顿饱饭都是很难的时候，谈什么事情，先谈吃饭要紧嘛！就这样子啊，是在这个情况底下，慢慢地成长起来的，然后再经过自己的错误，从文化大革命再往前算，从大跃进开始，如邓小平所说，我们耽误了二十年，说这个时候呢，我们在摸索里面前进，好容易现在由于邓小平最后他处理的方法的正确，总算目前现在开始走这条路了，不是吗？中国不是这样走来了吗？ <br />
<br />
&nbsp; &nbsp; 我李敖小的时候，亲眼看过日本兵骑着马，在我们的国家里面那样子耀武扬威，所以对我这个年纪说起来，一个强大的中国对我是非常重要的，对我的信仰是非常重要的。所以我在跟大家讲，我们中国讲富国强兵，什么叫富国强兵？大家看，中国的《二十四史》，就《二十五史》里面，《史记》里面的，《孟子荀卿列传》里面的一段话，他说当时的秦国，秦用商君，就是商鞅，富国强兵，这四个字太吸引我了。我们中国太穷了，谈不到富国，可是强兵很重要，为什么呢？因为从鸦片战争以来，中国人被洋鬼子打，我们中国国力太弱了，这个洋鬼子里包括东洋鬼子，包括日本人，所以跟我说起来，我们中国有一个武力，使全世界的洋鬼子、东洋鬼子不敢打我们，这个对我李敖太重要了，对我们中国也太重要了。今天享受到这种结果的人，知道不在话下，当然没人敢打我们，可是你知道我们付了多少代价吗？我们花了一百五十年的时间，花了千万人头落地的代价，才换得了最后中国还没有富国可是先强兵的这个境界，我们付了那么多代价。</strong><br />
<br />
&nbsp; &nbsp; 所以这个时候，我李敖才知道，至少中国共产党帮着我们强兵，在强兵的这种情况底下，没有人敢打我们，代价付的太多了，没有共产党照样付代价，多少人饿死了，多少人牺牲了，千万人头落地了，毫不稀奇，可是虽然我付了那么多代价，至少我们落得一个结果，至少有一个结果，就是强兵，中国强了，中国没人敢打我们了，中国强了，日本的军人不敢骑着马在我们国家里面耀武扬威了，这个对我太重要，我认为对中国人也太重要，就这样子啊。其它都不谈，什么成绩暂时都可以不谈，都不论，可是至少强兵做到了。现在由于邓小平的政策的一个展现，我们开始走富国的路线，不是先富国，后强兵，而是先强兵，后富国。当时中国的外交部长陈毅就讲过那句狠话啊，日本人奚落我们，说你们中国人连裤子都没得穿了，还要搞原子弹，我们外交部长陈毅就讲了一句虎虎有生气的话，就宁肯不穿裤子也要搞原子弹。所以今天，就要做到这一地步了，强兵做到了，然后富国，现在正在迎头赶上，不是吗？<br />
<br />
&nbsp; &nbsp; 这就是我才跟大家说的，我刚刚拿出来厚片薄片土司，厚片薄片黑胶唱片，告诉大家什么？告诉大家，第一代的革命先行者，他们最后取得了发牌的权利，就是取得了统治中国的权利，他们统治开始，才能够有富国强兵的结果，如果没有他们，靠着国民党这种政权，也没有富国，也没有强兵，什么都没有，至少今天靠着共产党，别的暂时不谈，至少强兵这一点，我们这么多年来做到了。鸦片战争以来，一百五十年以来，一百六十年以来，一百七十年以来，我们做到了，没有人敢跟我们玩了，洋鬼子不敢打我们，东洋鬼子也不敢打我们，我们做到这一点。我觉得很了不起的一点，就是由第一代的革命领导人、革命先行者，取得了中国发牌权，然后使中国走富国强兵的路，这里面按照邓小平的说法，走了二十年的错路，就算二十年的错路，我们剩下的时间还是富国强兵啊，还走这条路，现在走上这条路了。<br />
<br />
&nbsp; &nbsp; 所以对我李敖而言，我真的珍惜这个情况，并且我也知道，以一个历史家的判断告诉大家，靠国民党，到今天没有这个结果，这就是说为什么我在言论里面，我毫不掩饰地说至少共产党带来了强兵的结果，强兵的结果对一个从小看到日本鬼子骑着军中的大马，在北京城里这样子嚣张的人而言太重要了，我不要再看到那个镜头，我要中国强大起来，这就是今天我的一个基本的立场。如果有人说，你李敖偏袒中国共产党，骂国民党，我告诉大家，即使有这个现象，我也有我的理由，就是说，至少共产党带来了富国强兵，带来了强兵这个结果，富国呢，现在在走这条路，依靠中国国民党，我告诉你，依靠无能的中国国民党，什么都不是。至于说要依靠民进党，对不起，什么都没有了，什么都给你卖光了，民进党是个孬种的政权，怕美国人，怕日本人，谈都不要谈，这就是今天我跟大家所说的，我有我客观的论证方法。可是从感情上面，即使有人责备我，有所偏袒，我也欣然接受，对我而言，我一再讲过，我再也不要看到日本鬼子骑着军马在北京的街头耀武扬威了，我很快乐。<br />
