													<h2>响一声就挂电话 疯狂吸费内幕</h2>
						
						
																											<div id="postmessage_800770" class="t_msgfont">揭秘“响一声就挂”电话&nbsp; &nbsp;&nbsp;&nbsp;疯狂吸费内幕！<br />
 <br />
如今，几乎每个手机用户都接到过这种恐惧电话：手机忽然响起却又立即挂断。如果你回了电话，或是接入录音电话，或被挂断，或是忙音，或没有任何声音，瞬间电话费用却被疯狂吸走。在调查中，内幕人士说：利益分成上，运营商占了五成！结论得出了：回拨电话吸费诈骗幕后黑手，不仅仅有声讯台，还有运营商。 <br />
 <br />
一个歌谣道尽人们的无奈与骗子的疯狂：电话铃声响起，骗子开始耍你，一响就断陌生难辨；打回才知不对，手机已被吸费，几十上百不等，骗子屡屡得逞…… 这是一个怎样的陷阱？这个陷阱如何形成？<br />
 <br />
&nbsp; &nbsp; 响一声就挂害苦无数用户 <br />
 <br />
&nbsp; &nbsp; 公安部门提醒市民：“电话吸费诈骗是新型的诈骗形式……如果事主回电话，电话将被强行吸收事主话费，一次少则30元，多则几百元。” <br />
 <br />
&nbsp; &nbsp; 响一声就挂电话的收费也称为电话回拨吸费。事实上，此类骗术正在泛滥成灾。根据了解，目前全国只有西藏、青海等地方没有出现类似案件。在北京&nbsp;&nbsp;，仅仅2007年电话回拨诈骗案件报案次数就达2500余起，超过短信诈骗居手机诈骗首位。 <br />
 <br />
&nbsp; &nbsp; 与此同时，电话回拨吸费开始向固话蔓延。一位商铺老板说：“店铺电话经常有陌生电话打来，电话打来，如果不接吧，可能丢掉生意，毕竟店多，人家可能觉得你态度不好跑去别家了。接吧，可能上当。” <br />
 <br />
<br />
3月11日“掉进”了电话回拨的陷阱。那是在午夜，手机忽然响起却又立即挂断，回拨过去，却是一个声讯台：“你好，这里是香港，恭喜你中了一等大奖……”查询话费，回拨电话1分钟的代价是48元。 <br />
 <br />
&nbsp; &nbsp; 响一声电话公开招收加盟商 <br />
 <br />
&nbsp; &nbsp; 据了解，电话回拨吸费陷阱制造者大多在网上兜售骗术所需的技术设备。现在，更有甚者公然将广告打到了招聘报纸上。 <br />
 <br />
&nbsp; &nbsp; 看到了如下一则广告：“本公司长期经营加值型电话付费平台，现因业务发展需要，希望各地代理商踊跃加盟，坐在家里接电话就可以赚大钱。” <br />
 <br />
&nbsp; &nbsp; 随后联系上了这家公司的王姓经理。“实际上我们出售的是‘加值性付费业务平台’，只是外界现在都称为响一声就挂电话。”王经理对记者说，该平台每分钟将产生最少30元，多则几百元的收入。且未接电话回拨率可达30%。<br />
<br />
 <br />
“加盟费从8000元到4万元不等，这包括购买设备的费用和加盟费。公司会派人到加盟商所在地调试设备。”王经理说，每产生50元的话费，8000元的加盟商可拿8元提成，4万元的可以拿25元。”既然利润如此丰厚，为何还需要大量加盟商呢？记者表示了怀疑。王经理哈哈一笑：“‘加值性付费业务平台’虽然不违法，但毕竟不道德。现在我们搞分散加盟，金额虽然小了，风险其实也小了，大家其实更容易活得滋润。” <br />
 <br />
&nbsp; &nbsp; 王经理表示，目前已有许多加盟商加盟。“公司绝对有能力保证加盟商的安全。因为如果你的号段被运营商列入黑名单后，公司的技术人员会主动帮助你更换新的号段。” <br />
 <br />
&nbsp; &nbsp; 内部人士讲述回拨吸费伎俩 <br />
 <br />
&nbsp; &nbsp; 几经周折，联系上了一家电话回拨公司的内部人士，该人士给记者揭露了“回拨吸费”的详细流程。 <br />
 <br />
&nbsp; &nbsp; 该人士说，这是一条由五级单位组成的利益链，他们是三级单位。一级单位为设在美国或中国台湾地区的总部，二级单位是电信公司或移动运营商，三级单位负责出售电话群拨器，四级单位是电话回拨加盟商，五级单位就是广大普通手机用户，亦即受骗者。 <br />
 <br />
&nbsp; &nbsp; 这条利益链获取利益的途径是：四级单位首先到二级单位注册一个特殊服务号码(声讯电话号码)，然后四级单位从三级单位买回几台电话群拨器，通过电话转接器将声讯电话与手机、固定电话进行默认，电话群拨器自动拨号五级单位接通后理解挂断，如果五级单位回拨电话，电话将由加值电话付费通卫星转接器直接将打进来的电话转接到一级单位，由一级单位接听电话，每分钟有高额的利润回报，所有的支出都由消费者支付，二级单位的计算机平台会自动代收费用。 <br />
 <br />
&nbsp; &nbsp; 根据此前王经理的说法，加值电话付费通卫星转接器是美国施耐德公司及中国台湾中华电信共同打造的资费平台，同时该平台还和中国几大运营商签订了共同打造高收费业务。不过，这个说法均未得到国内各运营商的承认。<br />
 <br />
&nbsp; &nbsp; 那么为什么会形成高收费呢？它是将打进来的电话通过卫星转接器，自动转接到国外，有的甚至是多次转接，从而产生高额话费。 <br />
 <br />
&nbsp; &nbsp; 事实上，从技术角度来看，电话群拨早已可以实现。只要在机器上设置好需要拨打的手机或固话号码段，群拨机器就很容易地依次拨打这些电话，而且还可以按城市群拨，比如今天集中群拨北京&nbsp;&nbsp;，明天还可以改拨广州深圳。目前，类似的群拨器在深圳就能买到，而网上也有不少提供群拨服务的公司。 <br />
 <br />
&nbsp; &nbsp; 在利益分成上，比如每分钟产生50元话费，一、二级单位共抽取50%，3级单位作为代办单位抽取30%，最后落到四级单位手里还有20%。</div>

							
							
							
							
