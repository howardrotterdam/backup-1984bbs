													<h2>北京艺术家的非分之想</h2>
						
						
																											<div id="postmessage_490256" class="t_msgfont">北京艺术家的非分之想<br />
2010年02月23日 17:54 铅笔经济研究社 周克成<br />
<br />
因为拆迁纠纷，艺术家们走上了街头。应该说，在这场拆迁纠纷中，艺术家也是有一些道理的，比如签订了房屋租赁合同的，要拆迁就应该获得补偿，比如说拆迁一方不应该出手打人等等。<br />
<br />
但是，这些艺术家在这纠纷中确实也有一些非分之想，提出了一些荒谬、无耻和可笑的要求。<br />
<br />
一，新京报记者李健亚报道说：“所有艺术家都知道拆迁事态不可逆转，但艺术家也希望开发商能直面解决补偿款问题以及此后艺术家安置问题。”<br />
<br />
艺术家们在这里提出的“安置问题”就是非分要求。假如业主要提前解除合约，是可以要求他们赔偿，而且是严格按照之前签订的合同条款赔偿。但现在这些艺术家竟然要求开发商“直面解决”他们的“安置问题”，这不是很荒谬吗？赔你钱之后，你能不能找到新的住处，关那些开发商什么事？难道一个医生拒绝帮你看病，还有义务帮你找别的医生了？一个人拒绝卖面包给你，还有义务帮你找餐厅了？<br />
<br />
二，在接受新京报同一位记者李健亚采访时，北京艺术家、清华美院教授李象群说：“我的建议是，为了避免艺术家群体的流失，希望政府能规划出大片区域，解决这些拆迁的艺术家租户的去处。”<br />
<br />
这是一个无耻的要求。艺术事业在艺术家看来可能重要无比，艺术家的价值和地位在他们眼中可能也异常重要和特殊。但是，在其他纳税人眼中看来，未必是那么回事，未必值得由政府出钱资助和运用特殊政策扶持。<br />
<br />
试问，这些艺术家何德何能，竟然要政府用纳税人的钱去资助他们？如果他们真的对人们有贡献，那高价或大量出售他们的作品好了，这样他们就能活得很好，根本不需要政府的资助，正如许许多多已经成功的艺术家一样。<br />
<br />
如果他们是那么失败，失败到自己的作品根本就没人欣赏，没人愿意为之买单，那他们的工作也就毫无价值，并不值得政府用纳税人的钱去资助他们。只有那些失败的，头脑糊涂的，或者寻求作秀出名的艺术家，才会这样无耻地要求政府特殊照顾他们。<br />
<br />
可是北京这位艺术家提出的要求，竟然是要政府“规划处大片区域”，胃口还真不小啊！其无耻程度真让人佩服啊。<br />
<br />
三，在新京报的同一篇采访稿中，中央美院老师马璐说：“作为世界艺术中心之一巴黎，与北京有许多相同的地方。其吸引艺术家、稳定艺术家的方法是由市政府为艺术家提供一批廉租工作室和平价工作室。廉租工作室租给艺术委员会评选出符合标准的艺术家，保证一定的时限，但不得转租，到期后再作评定，收入低且艺术有发展的艺术家可以续租，收入高且艺术发展到高水平的艺术家可购买平价工作室。”<br />
<br />
这是一个非常可笑的要求。这些艺术家的要求不限于政府资助他们，他们还希望能让自己去考核别的艺术家。只有那些“符合标准”的艺术家才能得到政府提供的廉租房。而那些想得到政府廉租房的艺术家，也就得努力向他们的“标准”靠近。<br />
<br />
我不必刻意贬低这些艺术家的专业水准，但我知道艺术进步也是需要打破各种条条框框和人为禁锢的。但今天这些艺术家，想要做的事情正是通过禁锢他人创见的办法去挑选人才，你认为这会成功吗？正如今天中国文学、电影等领域一样，既然有了政府资助，既然要得到政府资助得听某些人的话，那也就别去想什么真正的发展了。最可笑的是，一些人在干阻碍艺术发展的事情的时候，正是打着“发展艺术”的旗号的。呵呵。<br />
<br />
简单来说，那些走上街头的艺术家说什么要“公民权利”，而这实际上并不是他们应有的权利。他们要争取的利益，不一定是他们应得的利益。至少我在上面说的三点，就绝对不是。要求政府补贴，不是“公民权利”。这种要求的实质，不过是让政府抢钱分给他们罢了。<br />
<br />
所以我可以毫不夸张地说，北京艺术家不过是在用一种无耻的方式追求自己的利益而已。他们走上街头，也不过是为了自己的无耻主张而进行的一种行为艺术，比较不同的地方只在于，他们富有作秀天分的本质决定了他们比较容易引起公众注意。各路维权英雄们，我劝你们把这点看清楚了。不是什么人上街都是为了公共利益，不是打着公共利益的旗号就真的是为了公共利益。 （2010年2月23日星期二）</div>

							
							
							
							
