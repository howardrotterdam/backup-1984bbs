													<h2>王力雄7月5日在Twitter谈新疆</h2>
						
						
																											<div id="postmessage_722143" class="t_msgfont">按：本文是我（@jinchang_)在2010年7月5号整理王力雄（@wlixiong)老师在Twitter上的发言成稿。<br />
<br />
1.什么是「新疆」？――最直接的解释是「新的疆土」。但是对维吾尔人，那片土地怎么会是他们「新的疆土」，明明是他们的家园，是祖先世世代代生活的土地呀！只有对占领者才是「新的疆土」。<br />
<br />
2.新疆――即使对中国也是个尴尬地名。既然宣称自古属于中国，为什么又会叫做「新的疆土」？御用学者绞尽脑汁，把「新疆」解释成左宗棠所说「故土新归」，却实在牵强，那明明应该叫「故疆」才对。何况早在左宗棠前一百年，那片土地就已经被清王朝叫做「新疆」了。<br />
<br />
3.信息不是真理，甚至不一定是真相。没人能比统治者得到更多信息，却不能说统治者了解了事物真相。历史让我们看到，即使是在殖民地过了一辈子的殖民者，又何尝懂得那里的人民。<br />
<br />
4.新疆位于亚洲中心，面积一百六十六万平方公里，占中国总面积的六分之一。对数字没感觉的人可以这样比较：新疆的面积相当于三个法国，六个半英国；甚至新疆的一个县――若羌，面积也接近六个台湾，十个科威特。<br />
<br />
5.截止二○○五年底，新疆人口为2010.35万，当地民族约占60.4%，维吾尔族923.50万人，占45.94%；汉族795.66万人，占39.58%；哈萨克族141.39万人，占7.03%；回族89.35万人，占4.44%；其它民族都不到1%。<br />
<br />
6.新疆五千四百多公里的边境线，与八个国家为邻（蒙古、俄罗斯、哈萨克斯坦、吉尔吉斯斯坦、塔吉克斯坦、阿富汗、巴基斯坦、印度）。在中国各省区接壤国家最多、边境线最长、对外口岸最多。<br />
<br />
7.新疆当地十二个民族七个信仰伊斯兰教，五个属突厥民族，而与新疆接壤的八个国家有五个伊斯兰国家，多个毗邻国家的主体民族也是突厥族。新疆有六个民族跨界存在，其中哈萨克、塔吉克、蒙古、俄罗斯四个民族，相邻国界之外就是同民族的独立国家。<br />
<br />
8.所谓「新疆问题」核心是「东土耳其斯坦」独立运动。上世纪曾两次建立「东土耳其斯坦国」。第一次是一九三三年十一月在南疆成立的「东土耳其斯坦国伊斯兰共和国」，维持了三个月；第二次是一九四四年十一月在北疆成立的「东土耳其斯坦国共和国」，长达一年半。<br />
<br />
9．新疆生产建设兵团是个全世界独一无二的组织类型。它是共产党进军新疆后，改编国民党的投降部队，加上部分共产党军队形成的。今天，「兵团」已成为遍布新疆的庞然大物，下属十四个师，一百八十五个团，总人口二百五十四万，其中百分之九十是汉人。<br />
<br />
10.兵团是新疆最大的汉人组织，列属正省级行政单位，与新疆自治区平级。新疆到处都有兵团「团场」――这个独特的词是指兵团的团级单位占有的「领土」。新疆基本每个县都会有一个甚至几个兵团「团场」，有些地名干脆就用团场番号命名，并且印在地图上。<br />
<br />
11.兵团有自己的城市乡镇，设有公安、司法、检察机构，有独立的户口造册、结婚登记处、监狱、科学院、银行、保险公司等，自成体系，不受地方政府管辖。我把兵团称为「新疆自治区内的汉人自治省」。邓小平把兵团说成「稳定新疆的核心」。<br />
<br />
12.中国宪法对行政区域的划分没有兵团位置，现实中兵团却有上百块「飞地」分布新疆全境，总面积达到7.43万平方公里 ，超过一个宁夏或两个台湾；而且还建立了一个新疆各级地方政府无权管辖、由兵团垂直领导、任命的政权体系。<br />
<br />
13.在北京下发的各种文件上，除了「各省、市、自治区」的抬头，还要单独加上「新疆生产建设兵团」，俨然它是并列的一个省。新疆本是进行「民族区域自治」的自治区，却在这个自治区域内插进上百块另外的「自治区域」，形成对原本自治区域的割裂。<br />
<br />
14.强调和强化汉人为主的新疆生产兵团充当新疆「看守」的思路，必然是要把新疆当地民族当作被看管的对象，兵团因此会成为新疆人民与当地政府的异己者，它受到当地人民敌视、遭遇地方势力抵制也就是正常的。<br />
<br />
15.一厢情愿指望用「中华民族」的人造概念把56个民族统成一个民族，再以民族主义使整个中国一致对外，然而只要中华民族的概念不被其它民族认同，被鼓动的民族主义就会被每个民族用来煽动和凝聚自己的人民，作为追求本民族独立的根据。<br />
<br />
16.中共治下人文传承割断，人文教育置于边缘。即使受过良好教育的新生代官僚，也多是单一化专业人才，有知识而无心灵，崇拜强大蔑视弱小，依仗的只有权力体系和权谋手段，擅长的唯有行政与镇压，动辄实施的严打、重典等，一时似乎有效，却治标不治本，甚至饮鸩止渴。<br />
<br />
17.人文精神的缺失使权力集团无法面对文化、历史、信仰、哲学等更为深入的领域，解决问题的方法诡诈却单薄，只能以应急救火的方式平息事件。而民族问题恰恰首先是人文问题，必须具有人文的灵魂才能找到正确之道。从这个角度看，中国民族问题走入死胡同有一种必然。<br />
<br />
18.一位维族朋友对我说「在小饭馆里吃饭的99%是维族，99%是自费，而去那些大饭店大吃大喝的99%是汉族，99%是公费」，当地民族的很多失落正是源于这种直观对比。的确，新疆的高档消费场所几乎就跟中国内地一样，周围都是汉人，说的都是汉语。<br />
<br />
19.第五次人口普查的数据，十年间新疆汉人数量增长比率为31.5%，新疆本土民族人口增长比率为15.9%。按照计划生育政策，汉人生育受到更严格的限制，但人口增长超出当地民族一倍，只能说明增长来源是移民。<br />
<br />
20.汉人移民多了，就会在日常生活的方方面面与当地民族直接面对，争抢当地资源，瓜分当地市场，民族之间的冲突就不再是形而上，而是与每人的切身利益和日常经验息息相关。这种情况就可能导致巴勒斯坦化的发生。<br />
<br />
