&lt;&lt;&lt;&lt;&lt;&lt;<br />
据说“维吾尔”的意思就是“联合”。这样的民族其实很多，汉族和满族，都是联合产生的。当然，广义上讲，各民族都是，比方说，蒙古族也是，不过蒙古族那种情况是“小联合”，只限于蒙古诸部，汉族、满族、维族是大联合，是跨越了民族的联合。比方说，羌族就是华夏民族的一个组成部分，但羌族也是一个独立民族。<br />
维吾尔这个民族产生时间很晚，据说是蒙古人和突厥人的后裔。<br />
算不算原住民，就看从哪年算了。不过，维族就算是原住民，也仅仅是南疆的原住民，而且从来没有独占过南疆。<br />
从血统上看，像汉族、维族这样的民族都是高度混血的，几乎不能用血统来说事了。但从文化上讲，如果我们从文明冲突的角度入手，那么穆斯林就是外来的。<br />
注意：民族主要是由文化和民族认同感来定义的，不是由血统来定义的。比方说，犹太人流散四方，重归以色列的时候，他们之间的血统差异在相貌上可以轻易地看出来，但不管这些犹太人的皮肤多么白（日耳曼血统）或多么黑（黑人血统），他们仍然是一个民族的人民。<br />
<br />
如果我们仔细考察一下南方汉人和少数民族的长相（根据长相可以推测血统），有些地方有这样一种现象，即：文化落后的部落归属汉族。究其原因，像苗族、壮族这样拥有鲜明文化特征的老牌民族，不会轻易被汉化，随着汉族文化的传播，这些民族仍然保持其特征。相反，那些文化更加落后的部落居民，反而会很快放弃自己的民族性，直接加入汉族。因为这些部落发源地比苗族、壮族更偏远，所以他们的长相就更加不像汉人。<br />
这就造成一些地区（比方说海南），本地汉人不像汉人，而少数民族反而像汉人的情况。（因为少数民族也是从北方来的，而本地汉人却是原住民。）<br />
这种规律，在维族诞生的过程中，也有表现。也就是说：没有自身鲜明文化特色的人，联合而成了维吾尔族；而那些有悠久历史的老牌民族，如哈萨克族，吉尔吉斯族（即克尔克孜族）反而成了相对少数。</div>

							
							
							
							
