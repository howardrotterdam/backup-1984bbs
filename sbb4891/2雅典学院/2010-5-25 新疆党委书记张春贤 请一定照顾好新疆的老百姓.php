													<h2>新疆党委书记张春贤 请一定照顾好新疆的老百姓</h2>
						
						
																											<div id="postmessage_638908" class="t_msgfont">看到自由新闻社版块“新华社隐瞒血照 防汉维大战”的新闻后回复不断，新疆的、内地的网友在里面争论不休，我觉得我们不应该刻意去宣扬种族仇恨，而应该为避免再次发生恶性事件去努力，毕竟这不是老百姓的错。很多人都认为新疆的汉族一定恨透了维族，下面我转来一篇网友的博文，来自新疆的北漠小冰，让大家了解一下部分新疆汉族的想法。<br />
=========================================================================================================<br />
北漠小冰文<br />
<br />
还记得二十多年前，那时候我还小，姥爷抚摸着我的头讲山那边的故事。我抬头看一满子全是戈壁，风景渐远牲口渐近，杂草堆积人际罕见。姥爷在打草喂羊，我在羊圈里面扮演小狼，时间流淌如小河，盖房子讨生活，有一天，忽然间姥爷不动了，我长大了。<br />
<br />
<br />
也记得大概十多年前，依旧是戈壁大漠奶茶烤馕，父亲付案写作笔根不罚偶然母亲会给我讲父亲从山西逃荒到这里，送牛奶的牧羊家的孩子腼腆的笑容，报纸上大大小小父亲报道的青河发展情况，一切都安逸的如同草场上吃草的老牛，悠然自得。<br />
<br />
<br />
姥爷从河南逃荒到新疆，爷爷从北京跟随王震去了新疆，母亲跟随姥爷，父亲跟随爷爷，最后又交错在一个荒芜人烟的青河县，一个荒凉的让人感到恐慌的地方，一转眼，父亲，爷爷姥爷的坟墓都堆在青河的后山。我看着身份证，才恍然想起童年的时光。<br />
<br />
<br />
我想我最有权利去谈家乡的发展，我每时每刻都在关注着那些牧民，关注着家乡的变化，关注那土地上的一切，在外面我总自豪的说，我是新疆人，如假包换的新疆人。<br />
<br />
<br />
1， 请藏富于民。<br />
<br />
依稀还记得曾经那大大的口号‘西部大开发’，此刻又看见各个门户网站大张旗鼓的专题展望新疆开发新疆，就让我觉得似曾相识，又恍然若失。曾经很多人踏上过这片土地，他们带走了财富和矿产，留下了贫困的人民。<br />
<br />
<br />
青河真的有发展，此刻你踏上那里，道路两边楼房鳞次，红绿灯走走停停，甚至你走到某一个人家都会觉得这生活水平提高了，老百姓有收入了。但是你一旦走远点，到各个乡村你就知道了，落后程度是不敢相信的（当然那里的领导会拿着数据告诉你每年都在增加，都变好了）。但是我老妈，一个第二代支援新疆的人，为了烤火费，打了三年的官司，他们本该享受新疆发展成果的人却连普通的人的生活都不敢奢求。<br />
<br />
<br />
请一定藏富于民，帮助那里的牧民，提高他们的生活水平，照顾他们的牛羊，让他们学会知识管理与走出去看世界。<br />
<br />
<br />
请一定藏富于民，看看每年的低保到底是那些人拿走了，请把国家补助的钱送到真正需要的手里面去，请一定监督好。<br />
<br />
<br />
请一定藏富于民，了解百姓的想法与困难，去切实的帮助他们，看看每年的国家补贴都到农民的手里没有，请一定先照顾好农民。<br />
<br />
<br />
请一定藏富于民，请把那些沦落到内地靠偷靠抢过日子的孩子都接回来，我们有全国最丰富的资源，有最美丽的风景，有全国对口支援，请让他们学有上，衣有穿，生活有改善。<br />
<br />
<br />
2， 请发展人文环境。<br />
<br />
新疆是歌舞之乡，却鲜有几个人站在全国的舞台上；新疆有那么多优秀的写手，但是新疆的出版社却难以发展。请让新疆的文学走向全国，请发展经济的时候，一定要照顾人文，让各民族彼此照顾，相互关心。<br />
<br />
发展民族文化，越是民族的，越是世界的，听取不同民族的声音，人文素质上去了，彼此才能了解对方的感情，才能够和谐相处。<br />
<br />
<br />
3， 请保护环境。<br />
<br />
在门户网站的专题上，我们又一看见在新疆某某地发现了矿产，但是我想说，我们的发展不一定非要以贩卖矿产资源为主，我还记得我们要走可持续道理，我还记得我们要健康发展我们的国家，新疆占据了全国六分之一的土地，但是别忘记了那里大部分是沙漠，沙漠会扩张，每年都在增加，请一定照顾好新疆土地上的一花一草，因为一花一草一世界，它们是最后抵御沙漠的生命，它们没了，就剩人类了。<br />
<br />
<br />
请一定让资源税都能照顾到当地的老百姓以后，在谈开发。<br />
<br />
<br />
4， 请大力发展旅游业。<br />
<br />
新疆有着全国最美丽的风景也有最独特的人文气息，新疆的美丽是可以融合的。走遍新疆都需要一个月的时间，新疆南北差异巨大，到南疆感受大漠，到北疆感受草原，你一定不会来到新疆，你只会后悔玩的时间太少了。<br />
<br />
<br />
戈壁上的小草，纷乱坚定孤傲的成长，谈谈的灰黄色点缀在戈壁黑色的背景中，如同黑夜中微弱光芒象征着生命的奇迹。风吹过细数的沙砾，岩石，刻画出千万年的沧桑，清清的夹杂着花香从古老的那头吹到原始的尽头。秃鹫飞过天空，看不见远去的痕迹。远处的驼铃声摇曳着睡梦中的呢喃。<br />
<br />
<br />
哈萨克族牧民赶着羊群骑着马。一代又一代的游牧生活让他们沉醉在夕阳美景下，他们没有痛苦的笑，没有兴奋的哭。<br />
<br />
<br />
作为新疆的一个写手，我愿意为家乡的旅游宣传做自己应该做的宣传。新疆的美丽是应该让全国人民都知道的，现在是宣传远远不够。<br />
<br />
<br />
想说的话很多，我会默默关注我的家乡，看了说明，黑龙江对口支援青河，两个高寒地区对接上了，请一定给那里的孩子一些足球，呃……也许我担心多了。<br />
<br />
<br />
请张春贤书记一定给老百姓更多的实惠，作为新疆人临表涕零，不甚感激。<br />
