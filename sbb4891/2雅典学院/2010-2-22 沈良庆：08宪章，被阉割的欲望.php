													<h2>沈良庆：08宪章，被阉割的欲望</h2>
						
						
																											<div id="postmessage_487908" class="t_msgfont">《08宪章》：被阉割的欲望<br />
<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 同西方世界近代以降那些堪称伟大的革命家如英国辉格党人、美国开国先贤和启蒙思想家如苏格兰启蒙思想家和法国启蒙思想家相比，中国这班先天不足、后天失调的启蒙知识分子不过是鹦鹉学舌、食洋不化的侏儒。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 我说《08宪章》缺乏政策意识还是比较温和、谦卑的批评。事实上，它并不缺乏政策意识，缺乏的仅仅是正确、可行的政策意识。不客气地说，启蒙知识分子及其影响下的人权民主运动和维权运动最缺乏的就是敌情意识。他们犯下的最大错误是把极权国家当成了威权国家，甚至于仅仅是存在一大堆有待改进的政治积弊和社会问题的正常国家。[40]如此一来，他们首先就把自己当成了这个国家的公民。在充当反对派时，潜意识中预先承认了极权国家及其法统的合法性，自己也成了带有怨妇色彩的所谓建设性反对派。殊不知，极权国家只有敌人，而无所谓合法存在的反对派。在此基础上形成了错误的进退失据的改良主义政策意识。“没有敌人”的过分温和、谦卑，使改良主义的政策意识失去了战略制高点，陷入无物之阵。导致无原则的和平主义、和解主义、反对一切暴力和廉价的宽容、博爱泛滥成灾，不仅自己无法制订和执行有效的对敌斗争政策，还专门搞窝里斗，干些亲者痛、仇者快的蠢事，客观上充当了极权国家的编外消防员。假如启蒙知识分子真要促使统治者实行自上而下的改良，也同样需要被统治者自下而上的压力。罗马共和国设立护民官以抗衡贵族权力的傲慢无忌，对于维护平民的权利和城邦的自由无疑都十分重要。而护民官的设立，恰恰是平民自下而上抗争的结果。那是一种什么样的抗争啊？站在贵族和城邦的角度来看，简直就是叛国资敌：当敌国即将来袭时，平民乘机哗变，携带武器集体逃离罗马城，啸聚山林，迫使元老院同意设立护民官。此举跟《大宪章》的产生同出一辙。许多人因此谴责罗马的混乱。就像刘晓波先生、余杰先生谴责类瓮安、杨佳事件一样。近代性之父马基雅维里独具慧眼，在《论李维》第4章赞扬“平民和罗马元老院之间的不和，促成了共和国的自由与强大。”[41]他是历史上第一位称赞党争有益的政治哲学家。这就是政治家的犀利眼光。共和国如此，摧枯拉朽以建立共和国同样需要丰富的政治生态，才有可能执两用中。一味乞求统治者改良，门都没有。即便要招安，也是如此。《水浒传》中的宋江想要招安，固然要把晁盖的聚义厅改为忠义堂，但仅有招安的欲望是远远不够的，还得有李逵那样的反贼（暴民）挥着两把大板斧（法西斯）直嚷嚷“杀去东京，夺了鸟位。”并能够打败高俅、童贯率领的剿匪大军。假如江南还有个剿而不灭的方腊，那就更好。否则，朝廷哪有兴趣招降纳叛。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 重要的是基本价值观的差异和如何才能消除这种差异。亚里士多德在《政治学》中指出：“人类所不同于其它动物的特性就在他对善恶和是否合乎正义以及其它类似观念的辨认[这些都由言语为之互相传达]，而家庭和城邦的结合正是这类义理的结合。”[42]具有某种包括正义感在内的基本价值观，是人类区别于其它群居动物的一个重要特性，人们正是在对包括正义感在内的基本价值观的共同理解基础上创造了一个城邦国家。甭管是共和国，抑或君主专制、一党专政国家。罗尔斯在《正义论》中津津乐道的所谓重叠共识，也必须建立在这种共同理解的基本价值观基础上。用他的话说就是“作为公平的正义”及其在逻辑上难以自洽的所谓词典式序列正义两原则（1.平等自由原则；2.差别原则）。[43]否则，重叠共识只能从空中摔下，跌成互不相干的碎片，甚至酿成不共戴天的流血冲突。至少在理论上，其右派同事诺齐克就无法跟他那套带有新自由主义色彩的正义论达成重叠共识，其左派同行布迪厄也未必同意跟他那套不够社会主义或者平等的正义论达成重叠共识。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 像奈特、西蒙、弗里德曼那样只相信自由的经济学家，为什么特别崇尚自由市场的价值？说白了，他们留给我们的思想遗产是以自由至上论这样的政治哲学为基础的。基本价值观的差异就是谁能独立战斗到最后的差异。这里所谓战斗，并不一定指使用暴力，也不一定排除使用暴力，要视具体的语言环境而定。但如果在既定文化背景下的理性人目标一致，他们就只需要找到能够更好地实现此类目标的行为方法。在这里，如何实现目标一致是很重要的。这就是自由市场的价值所在。弗里德曼把市场机制视为可以调和价值差异的装置，试图通过他所谓实证经济学的程序解决规范经济学的价值差异。在自由市场上，所有人持有的不同的价值与偏好都可以通过交换达成一致，从而获得相同的边际价值（从交易中获益）。如果没有自由市场，哪里会有一套君民共享的价值观？没有一套君民共享的价值观，社会如何稳定、和谐、不折腾？各方只好手持革命或者反革命的法西斯，没完没了的折腾。实行宪政民主的最大好处，莫过于为所有交易者提供一个合法的政治交易市场或者说政治斗争通道，使各方得以按照市场规则公开、公正、公平地进行和平、理性、非暴力的政治斗争。不管你是君子抑或小人、上智抑或下愚，都可以从中获益。否则，利益受损者将会退出市场交易。这就是自由国家的价值所在。实行一党专制的中国，恰恰缺乏一个合法的政治交易市场或者说政治斗争通道。没有一套君民共享的价值观，没有足够的作为不同价值公分母的硬通货，我们拿什么同中共当局做政治交易，让它关心民瘼，乖乖地通过渐进改良结束一党专制，实行宪政民主。赎买也得有资本啊！果不其然，刘晓波先生一厢情愿地怀揣《08宪章》，极其温和、谦卑地私闯权力垄断的禁区，刚一露头，就被当成走私犯连人带货通通没收。焚书而未坑儒，已经是改革开放的伟大成果了。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 仅仅有实行宪政民主、建立联邦共和国的欲望是远远不够的。太监何尝没有欲望，但他只能看着皇帝干着急。借用经济学术语就是需求必须转化成有效需求，才会有实在的消费。关键在于“如何可能？”孙子兵法云：“知彼知己，百战不殆。”倘若既不知彼，又不知己，那就很危险了。辛亥革命胜利后，孙中山既有革命党又有枪杆子，试图用《临时约法》约束袁世凯尚且不可能，面对强大、凶猛、狡猾的中共当局，既弱小又柔仁好儒的启蒙知识分子拿什么与之达成《08宪章》的改良共识？[44]或以为，“我们没有敌人”，是因为在敌我之间存在一个广阔的中间地带，那就是中产阶级。他们是政治社会的稳定力量，中流砥柱。说话的人连前现代的中国与现代国家都分不清。后极权时代的两极分化，决定了中国社会的阶级结构是X型而非纺棰型，夹在党官僚阶级（政治精英）及其附庸官僚资产阶级（经济精英）、御用文人（文化精英）和广大底层民众（暴民、顺民和愚民）之间的所谓中产阶级不仅十分弱小，而且缺乏独立人格。这种发育不良的“小资”，早在1949年以前的上海滩等大都会就不难看到。那时叫“布尔乔亚”，比今天的“小资”洋气多了。学贯中西的胡适也无法依靠他们成事。何况当时尚有两大政治武装集团可以让胡适游刃有余。坐山观虎斗总比落入一只老虎口中强吧。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 余杰先生说“刘晓波的道路就是胡适的道路”。可谓画龙点睛神来之笔，不仅一语道破《08宪章》的隐情，还一语击中它的软肋。尽管他只说出了上半句。我帮他补上下半句：胡适的道路就是失败的道路。注意，我并没有说胡适的人生道路失败了。随国民党逃离大陆是他人生道路最大的成功，比章伯钧、罗隆基、储安平等人强多了。我也没有说胡适的思想道路失败了。作为一个启蒙知识分子，他那卑之无甚高论的自由主义言说，仍在闪闪发光，经久弥香。我要说的是：胡适的改良主义道路失败了，对其拙劣的模仿更不会成功。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 胡适的改良主义道路之所以失败，并不是因为所谓救亡压倒启蒙的问题，那根本就是个伪问题。谁在救亡，救什么亡？在当时的社会历史条件下，根本不具备走那条改良主义道路的政治社会环境，它是一个生不逢时的早产儿。面对两大革命党（国民革命和共产主义革命）的武装对抗，面对国际政治中苏联对中共当局的支持和西方世界对国民党政府的假仁假义、袖手旁观、冷漠、自私与短视，夹在中间里外不是人的胡适们有什么资本走改良主义道路？他们既缺乏一个强大的中产阶级的有力支持，又缺乏国际社会的有力支持，本身又只不过是一小群满腹经纶、高谈阔论却手无缚鸡之力的白面书生。在大规模内战的生死关头，既无力改良国民党威权政府，促使其遂行宪政，更无力帮助政府击败共产党人的进攻，先安内而后行宪政。更糟糕的是，他们徒有务虚高蹈的改良主义意图伦理，却缺乏如何实现意图的责任伦理、切实可行的政策意识和执行能力。他们那套过于道德化，过于幼稚、自私和软弱无力的清高、清谈，不仅无助于对国民党威权政府实行改良，无助于帮助国民政府击败共产党人，反而有利于自由的敌人。[45]这种书生论政和干政是典型的添乱、帮倒忙和不负责任。尽管当时的自由派知识分子完全有权利论政和干政。但是，论政者和干政者也同样应该为自己的政治行为承担政治责任。至于后来国民党威权政府成功地实现民主转型，那已经是退守台湾很久已后的事了，不仅与大陆无涉，主要也不是自由派知识分子的功劳，而是党争即民进党和台湾民众全面抗争的结果。知识分子因为掌握着话语权并且迷信话语权，总是喜欢把功劳留给自己，把责任推诿给别人，尤其是实干的政治家或政客。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 窃以为，对包括胡适先生在内的历史人物的作用、贡献和不足之处应该作出客观公正、恰如其分的评价，既不能任意贬损，也不应该盲目拔高。即是其所是，非其所非。在这方面，胡适本人倒是率先垂范，给自己作出了恰如其分的评价。我的意思并不是说他真的直接针对自己作出了什么评价，而是说他对自己相对准确的角色定位，实际上就是对自己很好的评价。他作为中国近代史上的一个文化人，一个启蒙知识分子，对中国的学术和思想文化，特别是自由主义学术思想和文化，作出了巨大的贡献。对此怎么评价都不过分。没有必要苛求他在学术思想和文化方面一定要作出多大的原创性贡献，甚至没有必要苛求他在向国人兜售西方世界的新思想、新文化时，一定要对西方世界的学术思想、文化源流和社会风貌有多么全面、深入的理解。事实上，他热情传播的主要是当时在美国很风行的杜威及其通俗化的实用主义哲学思想和西方人特别是美国人的自由生活方式。他的贡献在于开风气之先，给国人带来了现代科学方法、民主精神、独立人格和自由思想。他在这方面可谓继梁启超之后的集大成者。后人即便比他认识得更全面、深入，甚至作出了自己的原创性贡献，那也是因为他所代表的那一代学贯中西的文化人和启蒙知识分子筚路蓝缕，为我们开出了一条新路。由于极权国家和党文化的荼毒，这条充满希望的中国自由文化之路及其命脉几乎中断。后人在黑暗中重新摸索，其艰难困苦的奋斗精神和探索勇气或许远远超过胡适那辈人，但是就学术思想和文化成就而言，目前尚无出其右者。<br />
但是就政治层面而言，胡适鼓吹的那条改良主义道路无疑是失败的。即便退一万步讲，假如当时改良主义道路真的能走通，中国在1949年前后就变成了像台湾今日那样实行民主的共和国，那也不可能是胡适的成功，而是再造共和的政治家或者说政客的成功。这不仅仅是政策意识问题，也是执行能力问题。说白了，胡适是个热衷于“谈政治”的文化人和启蒙知识分子，而非热衷于“搞政治”的政治家或者说政客，更不是擅长于“搞政治”的政治家或者说政客。毋宁说，他的性格决定了他的人生：这是一个很富有现代公民精神的道德君子（私人领域）和文化人（公共领域）。真正擅长“搞政治”的政治家或者说政客，不仅需要具备足够的政治勇气和力量，还需要具备足够的政治智慧和技艺，用马基雅维里的话说就是狮子的勇猛和狐狸的狡猾。温和、谦卑的胡适之先生能够认识并且做到这点吗？[46]据乡贤唐德刚先生在《胡适杂忆》中回忆胡适时介绍，作为身经百战的政治家的李宗仁先生对胡适的政治性格和能力有过精当的评价：“适之先生，爱惜羽毛。”在引用李宗仁先生的评价前，唐德刚先生认为：“胡适之先生既然基本上是一位恂恂儒雅、有为有守的白面书生，他是不能搞政治的，因为他缺乏搞中国政治主观和客观的一切条件。”认为胡适在主观方面缺乏的是：“没有大政治家的肩膀、中上级官僚的脸皮和政客或外交家的手腕；（请注意唐氏这句话）他甚至也没有足够做政论家的眼光！”进而评论道：“做个大政治家，在主观条件上像胡先生所具有的那种‘信道不移’的精神，只是最起码的条件。更重要的是还要有铁一般的肩膀，如此他才能顶住政治上的惊涛骇浪，泰山崩于前而色不变地负荷起天降大任，然后任劳任怨，为国为民，死而后已。”[47]如此说来，抗战期间让他担任驻美大使，无异于赶鸭子上架。既难为了他，更难为了水深火热的国家、政府和人民，说明了国民政府无奈、无为和无人。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 胡适甚至并不真正了解他长期生活于其中并热情讴歌的现代西方文明。唐德刚评论道：“适之先生却是一位有深厚中国传统文化修养的老辈学者。他丢不掉孔孟之道的包袱而又自诩为国际主义者。他不了解他终身颂之的所谓‘现代西方文明’里的‘生活方式’是以‘契约’、‘合同’、‘利害’、‘力量’、‘斗争’等深入人心的概念为基础的。所谓‘民主’，所谓‘容忍’（这是胡氏晚年政治哲学的精髓）只是力量、斗争、利害等均衡以后的契约行为。”他“在中国未做过官僚，在海外也未尝与洋人共事，因而他没有看出今日西方的社会――这个陈独秀所说的‘以小人始，以君子终’的社会里面，那些人与人之间有礼无让、刻薄寡恩的‘小人’的一面。他背负着礼让的教条，爱人以德，因而只看出西方社会里恂恂然的‘君子’之风。所以他老人家对‘现代西方文明’的维护，直到他一杯在手、含笑而终而后已。”[48]“我的朋友”适之先生确实天真得可爱吧！胡适之为胡适，正在于尚有自知之明，没有把“搞政治”当成自己的本业。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; “爱惜羽毛”固然无法“搞政治”，不明白政治技艺同样不能“搞政治”。因为搞政治和搞军事、搞法律一样，都需要高超的技艺理性。当年，试图闹司法独立的柯克和詹姆士一世之间曾经就国王是否应该享有审判权问题展开过一场著名的濠梁之辩。詹姆士认为法官固然有理性，国王也同样有理性，所以国王有权跟法官一样断案。柯克据理力争，强调所谓法官的理性是一种专业化的技艺理性，不同于詹姆士所说的那种一般意义上的理性。英美政治家很多都是律师出生，因为律师是从事实际工作的法律工作者，长期决战于法庭内外，谋求的是被代理人和自身利益最大化。律师实务要求他必须更注重实际效果而非抽象的法律原则。因此，他们很强调法律技艺。英国议会拟订的法案也不像大陆国家拟订的法案那样注重抽象的法律原则和完美的逻辑体系，而是就事论事，因而显得很具体甚至很琐碎。这里边，除了判例法与大陆法的区别，（毕竟英国议会法案也是成文法）其中一个重要原因就是这些法案通常出自律师之手，他们都是些注重实际效果而非抽象理论的人，因而更能满足政治社会的实际生活需要，更有助于保护臣民的生命、自由和财产。要求政治家或政客个个都是圣徒般的道德君子，不仅在专制国家做不到，在自由国家同样做不到。除非你是激进、浪漫、彻底的无政府主义者，根本否定政治国家、政治社会和政治生活的存在必要。这种不切实际的、过于道德化的苛刻要求，不仅实际上做不到，也根本不足取。因为它片面强调意图伦理而完全不考虑“如何可能”的工具理性要求，漠视责任伦理。道德家固然可以高谈阔论，政治家却必须面对实际问题，追求实际效果，谋求约束条件下的利益最大化。这意味着他可能必须抛弃妇人之仁而做出为君子所不齿之事。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 《08宪章》之所以有试图走改良主义路线的错误政策意识，恰恰是因为它没有意识到人权民主运动和极权国家基本价值观的差异。说白了，就是缺乏正确的敌情意识。那些启蒙知识分子到处矫情地宣称“我们没有敌人”，时不时还把被迫以暴抗暴的臣民（暴民）当成敌人，甚至是最凶恶的敌人（法西斯）。当然，我在这里强调人权民主运动应该树立明确的敌情意识，抛弃似是而非、毫无生命力的改良主义路线，旗帜鲜明地走上取消一党专制极权国家、实行宪政民主的革命道路，并不意味着我们必须走暴力革命的道路。还是那句话条条大路通罗马。明确了革命目标与革命道路之后，就革命手段而言，类似和平、理性、非暴力的斗争方式固然可以作为革命者的一个选项，并不排除中共当局在革命压力或内外交困之下主动自上而下或者自下而上（均指统治集团内部）挥刀自宫的可能性。假如仅仅通过上书就可以促使其挥刀自宫，我也乐观其成。尽管可能性几乎等于零。类似苏联、东欧那样低限度的暴力革命和类似光荣革命、法国大革命、独立战争那样的暴力革命斗争方式也是一个选项。就革命结果而言，“改良”即在一定条件下革命与反革命双方达成妥协的不彻底革命（如光荣革命）和彻底革命（如法国大革命、美国革命或苏联东欧那样的天鹅绒革命、颜色革命）都是革命者的可能选项。至于如何选择，则是机会主义问题，取决于革命党人对形势、条件诸因素的综合判断。窃以为，完全和平、理性、非暴力的斗争方式和暴力革命斗争方式目前都不足取。前者等于挥刀自宫，后者不具备条件，可能的则是前两种选项的结合。无论如何，选择不同斗争方式的革命者或者革命党都不应当相互排斥。选择和平、理性、非暴力方式的革命同志，更不应当排斥民间的激进维权活动和瓮安暴动、杨佳袭警那样的正当防卫行动。他们付出流血、牺牲的巨大代价争取自由，同时也为相对温和、谦卑的抗暴斗争涂上了一层厚厚的保护膜。应当谴责的永远是暴政而非对暴政的反抗，何况和平、理性、非暴力的斗争方式要取得成效，必须以强大的“革命压力或内外交困”为条件。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 从表面上看，革命手段和革命结果中的第一项选择似乎就是《08宪章》的选择，其实两者迥异其趣。《08宪章》要的仅仅是自上而下的改良或者说说是跪着造反（钦定革命）。革命手段的第一项选择是眼光向下，着眼于自主抗争和“革命压力或内外交困”，不会反对充满“暴戾之气”的“法西斯”。革命结果中的第一项选择则是敌我双方谁也不能消灭谁时相互让步的妥协结果，而非跪着造反获得的恩赐。就政治制度的根本变更而言，它并非改良，改良仅仅是既定制度的自我完善，可以完全出自统治者自身的需要，也可以是外界的压力的结果（包括外国竞争性压力和民间反抗性压力，更可能兼而有之）；其次，革命中出现流血、暴力至少是低限度暴力是常态，完全不流血、非暴力则是极其罕见的变态。印度争取独立的斗争并非完全和平、理性、非暴力，南非更是如此（非国大拥有自己的武装力量，和解也是双方长期斗争后的妥协结果，而不是单方面无原则的爱心泛滥）。苏联东欧亦然。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 几年前，我在一篇短文中指出，维权运动不仅需要温和的依法维权维度，还需对激进的以法维权维度。事实上，维权运动中不仅出现了大量街头政治的谈判方式，还出现了更激进的以暴抗暴方式。人权民主运动同样需要激进的维度，才能改变消极、软弱、被动挨打局面，形成丰富多彩的政治生态，催生出普通法宪政国家，实现以自由立国的梦想。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 历史上绝大多数革命，既不是少数革命党人阴谋发动的，也不是改良派所能够阻止的。中国的辛亥革命、法国大革命、俄国革命皆然。革命党人是实在国家严厉镇压和严密监控的对象。他们通常被迫龟缩在阁楼上、地下室、监狱里或者流亡海外，很难进行大规模的组织活动，遑论组织大规模武装起义。大多只是为革命提供背景氛围并在军队和/或民间起义后接管需要有人收拾的烂摊子。[49]辛亥革命前，除了像徐锡麟、汪精卫那样搞暗杀、扔炸弹，孙中山等人组织的起义都失败了。改良派的呼吁更没有能力阻止革命，革命者连军队、警察、监狱、法庭和绞刑架都不放在眼里，凭什么听你的鸹噪。真正有资格充当反革命的是统治者：要么满足或者部分满足革命者的要求以消除革命，这意味着牺牲全部或者部分统治利益，全面投降或者达成妥协；要么能够彻底镇压革命。作为既反对革命又不赞成反革命的第三势力，试图充当和事佬并从中渔利，也得以鹬蚌相争为前提。共和国的存在同样以至少两派以上的不同政治诉求、政治集团的存在为前提，而且还必须能够共存，即谁也不打算消灭对手或者没有能力消灭对手。如前所述，罗马共和国的内争与护民官设置就是把党争合法化、制度化、程序化而非消除党争，即通过开辟具有可预见性的公开、公正、公平的合法政治斗争通道来堵塞具有高度不确定性风险的非法政治斗争通道。真正的和平、理性、非暴力的政治斗争，只有在自由国家（共和国）才能实现。没有自由国家（共和国），就既没有宪法规则和程序，也没有完全和平、理性、非暴力的政治斗争通道。</div>

							
							
							
							
