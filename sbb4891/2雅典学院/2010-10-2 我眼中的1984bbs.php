																											<div id="postmessage_853093" class="t_msgfont">世界是这么的小，让我知道了中国还有1984bbs这么个有趣的论坛。于是我豪不犹豫的给可爱的老大哥张书记写了封表白书，于是我拿到了那高傲的邀请码。记得当时的心情就像拿到了结婚证一样，从此以后我也是1984bbs的人了。<br />
&nbsp; &nbsp;&nbsp; &nbsp;刚结婚的人都是幸福甜蜜的，我也享受在这如胶似漆的日子里，直到有一天我发现，我突然发现我呆不下去了。事情的起因是这样的。我的毛概课挂了，不是我学得不好（这个可以肯定，考试考发给我们的红册子原题），是我在课堂诚实地说出了老师的常识错误，记得当时的态度有点像《霸王别姬》中的红卫兵。我也不想那么的横，只是太想把自己的想法强加给他了，而且理所当然的认为他会接受。直到昨天看了《盗梦空间》才明白原来强加一个想法给别人是那么的困难，最终还是一场梦境!<br />
&nbsp; &nbsp;&nbsp; &nbsp;也许一件事情就让我呆不下去，那也显得太脆弱了。可毕竟前途重要，虽说中国的教育是这么的差导致你不能期望能给你带来什么方向。可人生是自己的，自己还是要为自己负责，那怕坏得一沓糊涂。可这么才能干点大事了，去革命，怎么可能，六四死的榜样那么多。不是没有勇气，在一个体制说死不死，不死却很死的国度，改变起来的阻力比一个要死的还难。去改良，去死吧。改来改去只是按照他的来改，你是不能改变社会主义道路的。当我知道纳粹是国家社会主义的时候，当我知道大部分的国家抛弃了社会主义的时候，我明白了我们的国家还在那里意淫。也许有人要问我了，什么都主张都没有那我们的国家怎么走下去了?!其实我们可以换一种思维，为什么一定要有一种所谓的思想我们知道我们的社会主义走下去了？！我们来看看那些发达的国家美国，英国，日本等，他们有所谓的华盛顿主义思想，丘吉尔思想，皇室思想来来指导吗？可以换种思维方式，我们不朝哪些方向走。至少我们走得更加自然，自由。<br />
&nbsp; &nbsp;&nbsp; &nbsp;说得有点多了，有人开始不愿看了，但我还是想把最后的话说完。当我第一次看我的组员们留下那么幽默，天才的留言，我觉得他们是上帝派来消灭共产党的。但那些很多重复的话出卖了他们，他们只是一群自以为智商有优越感的牢骚满腹的乌合之众。凭这些小智慧是、是成不了大事的！有人说好的东西是聪明的人下笨功夫整出来的。我信这句话！这不是有要呆的地方，有个更好的远方在等我，那里有诗和远方......</div>

							
							
							
							
