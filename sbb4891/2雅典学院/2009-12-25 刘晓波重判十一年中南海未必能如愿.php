													<h2>刘晓波重判十一年中南海未必能如愿</h2>
						
						
																											<div id="postmessage_395550" class="t_msgfont">作者：刘路 <br />
&nbsp; &nbsp; <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;出乎绝大多数人的意料，北京当局居然重判刘晓波11年！而通常煽动颠覆罪都是在5年以下量刑，即使被冠以“罪行重大”也很少有人超过10年。刘晓波的罪证是6篇文章350字和《零八宪章》，我们知道含有被当局认定为“煽动颠覆”文字的异议作家谁人找不出6篇文章350个汉字？参与签署《零八宪章》的也早已达到上万人，组织和推动宪章运动的也不少于百人，为什么只抓一个刘晓波，并重判11年？任何一个哪怕对中共抱有最善意理解的人都不能不认为，这就是一个政治审判，没有任何法律的因子在起作用。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;所以，晓波先生此前对律师表示无论判多少年都不上诉，晓波先生在聆听宣判时向他的妻子微笑致意。因为他知道，这个国家根本没有真正的司法，这个政权也根本不把宪法和法律当回事。他们不过是用法律来包装权贵者的意志，用装模作样的司法程式来践踏宪法所保护的公民权利。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;得知晓波被判11年的消息，王丹先生在推特网上激愤地说：“1996年10月，我也是因为“煽动颠覆政府”被判刑11年。13年过去了，连刑期都一样。那些说中共已经进步了的人，不是瞎子就是骗子。”<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;的确，从最本质的意义上说，在对待异议人士的迫害，对民间独立思潮的打压这些方面，13年来，甚至20年来，当局没有什么进步，在言论的封锁和控制方面，得益于高科技和庞大的金钱支持，它甚至做得更彻底、更严厉。从这些纬度来考量当局的“进步”，只能是0分。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;在异议人士的阵营里，长期以来存在着激进派和温和派的分野，包括笔者在内的很多知识分子都曾纯真地认为，中国应该走和平理性非暴力的道路，通过完善法治、保护人权、推行渐进式的和平演进，减少震荡，避免动乱，经过一段比较合理的时间过渡，实现中国的和平转型，这也就是《零八宪章》描绘的路线图。这本来是一个对国家民族人民福祉负责任的切实可行的新演进方案，只要不怀偏见，不将自己的特权利益置于国家前途民族未来之上，都很难对这样的一份政治诉求提出反对，更谈不上妄加刑罚。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;但是我们高估了共产党的政治智慧和社会良心。我们并非已经忘记他们的政权是建立在亿万人的累累白骨、滔滔鲜血之上，我们也并没有忘记20年前天安门广场上彻夜的枪声和冲天的火光。《零八宪章》的发起人者们虽然知道它们的罪恶万死难恕，还是搞出“转型正义”的制度设计，给它们以出路，以自新的机会。但是我们错了，共产党根本不去分辨什么激进派、温和派，不去分辨什么山羊绵羊。2006年它抓了激进的高智晟，判三缓五然后让他人间蒸发，2009年它就判温和派的刘晓波，扔进大牢11年叫他彻底失声。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;直到今天，我们悲哀地看到，国内比较有点影响力的异议人士，不管是激进的还是温和的，大都被关押的关押，流放的流放，留在中国大陆还能产生一定影响的已经所剩无几。中国共产党梦寐以求的和谐社会，似乎就要实现了。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;重判刘晓波11年，北京当局的如意算盘不难看出，就是要在2020年之前，保证“和谐”，维持“稳定”。另一个算盘是“擒\擒王，杀一儆百”，可是，中南海的衮衮诸公们可曾想过，每年10万多起的群体事件，燃遍城乡的反抗怒火，有哪一件是刘晓波们发动的？看看法庭外面飘舞的黄丝带吧，想想中国第一个大独裁者秦始皇的教训：“坑灰未冷山东乱，刘项从来不读书。”你们打掉了晓波等讲理的文人，不讲理的“刘邦项羽”们就会用另一种方式跟你们说话！<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;北京当局自以为得计的算盘，还可能导致另一个后果，就是高高竖立起刘晓波博士的道义形象，帮助他在明年一举拿下诺贝尔和平奖。虽然对晓波而言，诺奖不诺奖不是他的追求目标，但是对中国的异议运动来说，这个奖却意义重大，因为它将凝聚我们的力量，凝聚全世界的道义支持，最终摧毁共产党“一党独裁万世一系”的迷梦。这恐怕是中南海的当国者们所料想不到的。 <br />
