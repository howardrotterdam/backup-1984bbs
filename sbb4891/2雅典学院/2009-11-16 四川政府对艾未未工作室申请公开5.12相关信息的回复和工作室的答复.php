<br />
1、 研究国家公安部门在震后抗震救灾中发挥的作用。<br />
<br />
2、 研究震后公安部门对遇难者名单、身份的处理过程及相关事宜。<br />
<br />
3、 研究震后公安部门开展维稳工作的成效与其负面作用。<br />
<br />
4、 监督各级公安机关用于维稳方面的费用开支，用途，并予以监督。<br />
<br />
 <br />
<br />
希望获得你们的理解和协助，谢谢。<br />
<br />
 <br />
<br />
