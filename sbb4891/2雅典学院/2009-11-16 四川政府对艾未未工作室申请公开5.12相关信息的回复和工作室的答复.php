																											<div id="postmessage_333230" class="t_msgfont">对四川公安厅《获取政府信息补充申请告知书》的答复：<br />
<br />
 <br />
<br />
四川省公安厅信息公开的负责人：<br />
<br />
 <br />
<br />
我于2009年11月13日收到你厅的《获取政府信息补充申请告知书》，该告知书中称“根据《中华人民共和国政府信息公开条例》第十三条规定，请你补充提供申请获取信息具体用途的相关证明材料。请于收到之日起10日内提供，无正当理由逾期提供或者不提供，我们将依法作出处理。”<br />
<br />
 <br />
<br />
自2008年5.12 汶川地震以来，我作为一名志愿者一直从事有关汶川地震和学生死亡情况的调研工作，这项调研涉及教育、民政、建设、公安、地震等领域与地震相关的信息和政策。为了履行宪法赋予一个公民的权利和义务，完善我们的调研工作，根据国务院的《政府信息公开条例》，我提出信息公开的要求。<br />
<br />
 <br />
<br />
《中华人民共和国政府信息公开条例》是保证社会进入民主和法治的基本措施，也是尊重维护个人权利的基本体现。在此条例中，明确规定了政府公开信息的义务，和公民获取信息的条件。该条例第十三条中明确说明：“除本条例第九条、第十条、第十一条、第十二条规定的行政机关主动公开的政府信息外，公民、法人或者其他组织还可以根据自身生产、生活、科研等特殊需要，向国务院部门、地方各级人民政府及县级以上地方人民政府部门申请获取相关政府信息。”<br />
<br />
 <br />
<br />
我在信息公开申请函中已经注明：“所需信息用途为1、掌握5.12地震真实情况和相关依据，作为调查和研究的依据；2、了解政府工作情况和提高政府工作透明度，行使公民的知情权、参与权和监督权”并且附有本人身份证复印件以证明本人中华人民共和国公民身份。<br />
<br />
 <br />
<br />
