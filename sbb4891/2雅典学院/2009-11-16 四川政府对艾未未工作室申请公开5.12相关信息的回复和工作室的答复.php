													<h2>艾未未对四川省教育厅《获取政府信息补充申请告知书》的答复（已于11月12日寄出）</h2>
						
						
																											<div id="postmessage_328797" class="t_msgfont">艾未未对四川省教育厅《获取政府信息补充申请告知书》的答复：<br />
<br />
四川省教育厅有关信息公开的负责人：<br />
<br />
我的名字是艾未未，自2008年5.12 汶川地震以来，我一直从事有关汶川地震和学生死亡情况的“公民调查”调研工作，这项调研涉及教育、民政、建设、公安、地震等领域与地震相关的信息和政策。<br />
为了履行宪法赋予一个公民的权利和义务，完善我的调研工作，开展我在艺术领域的创作活动，根据国务院的《政府信息公开条例》，我提出信息公开的要求。<br />
<br />
每一个现代公民的健全生活、生产活动，取决于对于他生存的社会信息的理解和知情状态，准确性的理解。个体的公民是国家或人民政治构成的基本的成员，获取准确的信息，行使知情权是公民作出正确判断、行为选择的前提，是生存不可缺失的先决条件。“享有公民及政治自由和免于恐惧和匮乏的自由”是人类所共同认同的基本权利的一部分。<br />
个人的完整是社会的基础政治形态，是人权理念和人的社会属性的基本受体，是社会政治、经济、文化的基石和社会文明进步的最终目的。<br />
一切有关自由的努力都是以个人的自由为目的，为了实现这一自由，实践社会的公平和正义是必然途径；为了实现社会的公平和正义，公民的知情、监督，参政权是必然手段。<br />
政务的坦诚、透明是社会公平不可少的保证。没有宪法和相关法律的保证，没有个人为自己的权益作出的努力和抗争，没有公民监督政治权力的制度保证，势必导致极权和腐败的发生。<br />
《国务院提出的信息公开》是保证社会进入民主和法治的基本措施，也是尊重维护个人权利的基本体现。在此条约中，明确规定了政府公开信息的义务，和公民获取信息的条件。<br />
要求以“信息用途”作为信息公开的条件，要求申请者对其“申请的用途做相关证明”的解释，是对国务院《信息公开》条例精神的曲解和变相抵制，它有悖于对公民与生具有的基本的权利的理念，与宪法的“任何公民享有宪法和法律规定的权利，同时必须履行宪法和法律规定的义务”理念相违背。<br />
要求公民“提供申请获取信息具体用途”同样是对联合国《人权公约》的违背。“对人类家庭所有成员的固有尊严及其平等的和不移的权利的承认，乃是世界自由、正义与和平的基础，确认这些权利是源于人身的固有尊严，确认，按照世界人权宣言，只有在创造了使人人可以享有其公民和政治权利，正如享有其经济、社会和文化权利一样的条件的情况下，才能实现自由人类享有公民及政治自由和免于恐惧和匮乏的自由的理想。”<br />
限制人们的知情权，就是限制的人的自由选择权和自由发展的权，这是掩盖的手段也是它的罪恶的本质。因为这个世界上的所有罪恶都是源以剥夺个人的自由发展为前提的。<br />
为了证明我的工作，我提供了我们以往的工作在公共媒体中的报道。<br />
希望获得你的理解和协助<br />
<br />
<br />
公民调查&nbsp;&nbsp;艾未未<br />
