													<h2>柯丹对中国地震局《获取政府信息补充申请告知书》的答复(已于11月24日寄出)</h2>
						
						
																											<div id="postmessage_339996" class="t_msgfont">中国地震局有关信息公开的负责人：<br />
<br />
地震局11月19给我发的《补充申请通知书》已收到，我感到十分诧异。<br />
一、地震局两次回复前后矛盾。<br />
10月28日我寄出信息公开申请，地震局在11月2日给我发出了受理申请的回执（中震[2009]第1号--回）：“经审查，你的申请行为符合《中华人民共和国政府信息公开条例》第二十条规定，本机关予以受理。 根据《中华人民共和国政府信息公开条例》第二十四条，对你的申请本机关将于2009年11月19日前作出书面答复。如需延长答复期限，将另行书面通知。2009.11.2”<br />
可见，该申请已经被审查并受理了，在15天后你局再提出“补充所需信息的具体用途说明后再行申请”的要求，相当于自己否定了自己上次的审查和受理。<br />
我感觉这种做法有两点不妥，一是出尔反尔，与国家级机关形象不符。二是耽误申请人时间。15天过去了，申请人不仅未收到答复，还需要补充具体用途后再行申请一次，这无疑与《中华人民共和国政府信息公开条例》中提到的信息公开要及时、便民的原则相悖。 <br />
<br />
二、关于地震局提出的“补充所需申请的具体用途”的回复<br />
在上次申请中，本人已经明确提出“所需信息用途为：1、掌握5.12地震真实情况和相关依据，作为调查和研究的依据；2、了解政府工作情况和提高政府工作透明度，行使公民的知情权、参与权和监督权”。 <br />
我认为，信息公开最根本的用途是公民对政府工作进行监督。作为一个现代民主政府，接受人民监督是基本的职责。而信息公开、公民知情则对行使监督权的的基本前提。监督政府工作的证明就是中华人民共和国的身份证，这本人在上次申请中已附上。<br />
另外一个用途是用于本人的调查研究。5.12地震中，超过9万名同胞遇难失踪，财产损失巨大，这是一场令人震惊的民族灾难。本人在地震后花好几个月时间参与5.12公民调查的调研活动，这项调研涉及教育、民政、建设、财政、公安、地震等领域与地震相关的信息和政策。国家地震局在防震减灾中所起到的作用也成为公众关注的焦点，也是我们调研的内容之一，该调查包括防震减灾规划、地震监测和预报、预防、抗震救灾工作等方面的工作。本人此次申请主要用于上述研究。<br />
本人认为，政务的坦诚、透明是社会公平不可少的保证。《中华人民共和国政府信息公开条例》是保证社会进入民主和法治的基本措施，也是尊重维护个人权利，希望地震局能按照该条例的要求，尽快对公众公开地震相关信息。<br />
为了证明我们的工作，在此我提供一份以往的工作在公共媒体中的报道（见附录）。<br />
希望获得你的理解和协助<br />
<br />
