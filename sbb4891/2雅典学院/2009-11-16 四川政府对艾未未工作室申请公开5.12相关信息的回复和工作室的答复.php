													<h2>徐烨对四川省青少年基金会《获取政府信息补充申请告知书》的答复(已于11月16日寄出)</h2>
						
						
																											<div id="postmessage_333232" class="t_msgfont">四川省青少年基金会：&nbsp;&nbsp;你好<br />
<br />
 <br />
<br />
 <br />
<br />
兹于2009年11月13日收到你回复的关于获取信息公开补充申请告知书。<br />
<br />
我的信息申请行为符合《中华人民共和国政府信息公开条例》第九条、第十条、第十一条、第十三条中你部门应当主动公开范围。<br />
<br />
第九条　行政机关对符合下列基本要求之一的政府信息应当主动公开：<br />
<br />
（一）涉及公民、法人或者其他组织切身利益的；<br />
<br />
（二）需要社会公众广泛知晓或者参与的；<br />
<br />
（三）反映本行政机关机构设置、职能、办事程序等情况的；<br />
<br />
