													<h2>奉上原文，我以后还是做一个围观的P民算了</h2>
						
						
																											<div id="postmessage_691987" class="t_msgfont">：《很多学生死了》为何柴玲, 吾尔开希, 李录等发起人却还活着?<br />
１９８９年，中国的经济改革正面临物价改革的瓶颈口，通货膨胀，物价上涨很快，老百姓情绪不满，而在这同时，许多高干子弟利用经济改革的机会，搞官倒，很快就把国家和人民的财富聚集在他们手中，这更加激起老百姓对政府的不满。所以当柴玲和王丹为首的学生，在天安门广场绝食反对贪污，反对官倒时，很快得到北京市民的支持。而且事态迅速扩大，并得到留美几万留学生坚决支持。 <br />
<br />
　　但是在后来运动就逐渐的变了性，柴玲，吾尔开希与李录的 人性的阴暗和丑陋 的一面也逐渐暴露出来。在第一次与当时的李鹏总理谈判时，李鹏答应政府调查官倒和贪污。但是以柴玲为首的学生代表拒绝撤出天安门。柴玲说。撤出天安门广场后，李鹏会对学生代表秋后算帐。在第二次与当时的李鹏总理谈判时，学生代表增加了不许对学生秋后算帐的条件李鹏也答应了。可是但是以柴玲为首的学生代表仍然拒绝撤出天安门广场。柴玲说我们不相信李鹏。当然，李鹏作为中国太子党的总代表，很难相信他会认真调查官倒和贪污。可是继续占领天安门广场就会给李鹏以破坏社会秩序的名义把这场反对贪污，反对官倒的运动打下去。柴玲，吾尔开希与李录难道不明白这一点吗？他们的智商就那么低吗？ 为什么共产党的幕僚一个一个走马灯似地劝告他们撤出天安门广场，他们也坚决拒绝呢？ 他们一点也不幼稚，他们是中国近代历史上最精明的学生领袖。 <br />
<br />
　　香港支联会从这场运动一开始就介入了。学生募捐的钱９０％来自香 港支联会。香港支联会驻京代表成了学生领袖的参谋。是不是由于港支联的意思，柴玲，吾尔开希等才拒绝撤出天安门广场？有迹像表明这一点。<br />
<br />
　　请看柴玲，吾尔开希等人的第一次逃跑：１９８９年５月１９日晚上，李鹏宣布北京戒严。柴玲，吾尔开希等高自联的头头吓得一人发了一千元，赶紧逃命。当时柴玲宣布绝食团的使命已经结束。要大家赶快疏散。他们把这一千元叫作保命费。所以５月２０日和５月２１日，柴玲，吾尔开希，李录，刘刚等许多高自联的头头都离开了天安门广场，但也不在家里。可是在５月２２日，他们又回到了天安门广场。这两天，他们在那里呢？怎么又不逃跑呢？另一个敏感的问题是：他们当初准备往那里逃跑呢？有一个高自联常委的话漏了天机：他骂道“香港支联会真不是东西，是支持我们，现在捅了漏子，就不管我们了。”在５月２０日和５月２１日这两天里，有些高自联头头要求港支联协助他们逃亡美国，但是港支联不肯答应。为什么不肯答应？港支联说，仅仅戒严令还不足以说明你们有危险。换句话说，还没死人呢！鉴于柴玲的逃跑，她被撤除高自联总指挥的职务，由王丹任总指挥。 <br />
<br />
　　５月２４日在王丹的主持下，高自联常委会通过了定于５月３０日全体学生撤出天安门广场的决定，并且通知了中国留美学生联谊会和香港支联会，要他们在５月３０日前游行一次。（中国留美学生就于５月２８日，在纽约，华盛顿，芝加哥，洛杉矶等地举行了大游行。)港支联眼见北京学生运动越出了它的操纵，就派特派员紧急飞北京。他与柴玲，吾尔开希，李录等秘密会谈，作了一场丑恶的交易。<br />
<br />
　　首先，由柴玲发难，猛烈攻击王丹是右倾分子，投降主义。经过高自联常委会的紧急会议，推翻了原定于５月３０日全体学生撤出天安门广场的决定，同时撤除了王丹的职，由柴玲重新担任高自联总指挥。此外，柴玲又在学生运动的口号中加上了“要李鹏下台”的一条。这就是使学生运动打上了死结。人们要问为什么柴玲，吾尔开希，李录有这么大的能量呢？一是由于他们控制了募捐来的钱，二就是他们与港支联特别关系。一些人支持柴玲就是想一举跳向美国。王丹当时说”中国的民主需要长期斗争，不是一天两天能成功的“王丹当时还说过“这次运动的目的已经基本达到，反官倒和反贪污口号已经深入人心，政府在新闻控制上也有所开放。继续占领天安门广场，不但会有学生流血的危险，而且会葬送这次运动的已经得到的成果。”王丹的这些非常正确的观点被柴玲，吾尔开希，李录批判为右倾投降主义。在政府答应了学生的”要对官倒和贪污进行调查“的要求后，学生们继续占领天安门广场，就显得理由不足。 <br />
<br />
　　在政府宣布戒严后，学生们仍然继续占领天安门广场，就是公开的对抗法律。在一些国家，学生们用占领广场或占领政府大楼的方法导致政府下台例子是有的。但是，那都是由于在美国的压力下，那些国家的军队不支持政府的原因。但是这在中国绝对不会发生。当有人把刀架在空中，要砍你的脖子时， 去争论对方是否有道理砍你的脖子上是愚蠢的。当务之急是把你的脖子移到安全的地方去，等他刀放下来，再与他争论道理。当时柴玲是这么说的：“现在那么多政府的官员一个个走马灯似地劝告我们撤出，那是为他们着想，他们怕事情闹大了丢官。他们怎么不为我们想想？如果全体学生撤出天安门广场，李鹏就会轻而易举的把我们抓起来。就是不抓起来，我们有好果子吃吗？我们与李鹏已经誓不两立，不是鱼死就是网破。”柴玲的这番发言很清楚地表明她是用天安门广场上的广大学生掩护自己。柴玲是用几千个脖子去掩护她一个脖子。<br />
有人说６４学生领袖幼稚，听听他们的讲话，幼稚吗？从这时开始北京学生运动已经完全变成港支联向北京政权发难的工具。港支联就是要制造北京的流血与死亡。他想用北京的流血与死亡来引起香港居民对97回归的恐怖感，甚至想借此让国际社会阻止中国收回香港。港支联首先买了很多帐篷，让学生晚上睡好觉，白天也好休息，这就留住了大批外地学生。外地学生主要来自辽宁和天津的高校。此外还发钱下去给各校代表去买吃的。<br />
<br />
　　但是仅仅有吃有睡还不足以留住学生，还必须消除学生对戒严的恐怖感。所以港支联通过柴玲，吾尔开希，李录等高自联的头头制造了一个又一个假消息，以蒙蔽学生和市民。其中最严重的是假造了所谓叶飞等七名将军，向中央提交“以老军人的名义，认为人民军队是人民的子弟兵人民军队绝不能对人民开一枪。我们建议军队不要进京”的意见书。这个消息是由天安门广场高自联广播站首先广播的，又由高自联发到中国留美学生联谊会。这是彻头彻尾的捏造消息。如果不是高自联捏造的，请柴玲，吾尔开希，李录回答：你们从那里来的消息？在这同时，还捏造了所谓７０％以上的副部级，８０％以上的司局级都同情学生。中共５００多名将军，更有３００至４００对军队进京表示反对的假消息。 他们是怎么捏造这些假消息呢？一个曾任高自联宣传组的女学生说：有一天，李禄给了她许多名字和电话号码，李禄说这都是老干部，有的还是副部级和将军。李禄叫她打电话采访。每一个人都问两个同样的问题： <br />
　　1) 对学生运动支持不支持？ <br />
　　2) 对戒严部 队如果向学生开枪如何看法？<br />
<br />
他说所有的回答几乎都一样 <br />
　　 1)他们都说坚决支 持学生的正义行动 <br />
　　 2)他们都坚决反对戒严部队向学生开枪。<br />
<br />
　　但是也几乎所有 的人都劝告她，学生还是先撤出天安门广场的好，有意见以后再谈。这位女学生说:有好多电话还是秘书回答的，她把所有电话记录都给了李禄。经过高自联头头的修改，就变成了所谓70%以上的副部级，80%以上的司局级都同情学生。中共500多名将军，更有３００至４００对军队进京表示反对的假新闻消息。并且删除了这些干部都主张学生先撤出天安门广场的忠告。老军人的意见书更是无中生有。中国的老将军们从来不兴搞联名书之类的政治活动。来自不同体系的军头更是老死不相往来。如果是老邓决定要戒严，没有一个军头敢说不字。高自联的头头制造了这些假消息的目的是企图给北京市民及天安门广场学生们一个中国军队内部意见不统一，李鹏地位不稳，军队不会开枪的假像。这就叫舆论导向。 <br />
<br />
　　稍微有点法律常识的人都知道，对抗戒严法的后果是死亡。在六十年代初，美国阿肯色的小石城，也就是当代美国总统克林顿的老家，白人种族主义者在开学时，不让黑人学生进学校读书，引起了一场全城性的骚动，由于骚动规模很大，政府无法控制，就实行了全城戒严。戒严期间，一些黑人团体成员违反戒严法继续进行示威游行，而遭到军警的枪杀。后来阿肯色的议会经过讨论，法律规定不许黑白学生分校。这场骚动才平静下来。事后，黑人团体要对枪杀示威游行者的军警起诉，州政府的回答是，死者是违反了戒严法，军警的开枪是合法的(justified)。前几年，洛杉矶由于黑人骚动而戒严时，军警对违反戒严法者也是格杀不论的。许多天安门广场的学生家长们要他们子女撤出广场是怕子女被打死。许多学校的老师们劝告学生们撤出广场也是他们被打死。许多政府干部劝告学生们先撤出广场再说也是从他们的安全作想。可是天安门广场的学生们，只听头头的话。他们对头头们捏造的假消息坚信不疑，把戒严法当儿戏。<br />
有人为柴玲，吾尔开希，李录辩护说“他们推翻了原定于５月３０日全体学生撤出天安门广场的决定是寄期望于万里通过人大取消政府的戒严令。“请看历史事实：５月２８日，当时的人大委员长万里在加拿大接见了美国和加拿大的中国留学生代表。留学生代表狠狠告了李鹏的状。说戒严法没有经 过人大批准是违反宪法的，并要求万里取消政府的戒严法。万里答应一回国立即召开人大紧急常委会研究戒严法的合法性。一个很重要的事实是万里从来没有对留学生代表说戒严法是非法的，只是说研究研究。在中国的官僚制度下，研究常常是推托的代名词，所谓研究就是研而不究。另一个更重要的事实是万里说了”为了防止意外，建议学生还是先撤出的好“他要留学生代表转告。北美中国留学生联谊会立即把与万里谈话的详情FAX给北京高自联。但是谈话一到北京全走了样。高自联的头头首先把万里说的”为了防止意外，建议学生还是先撤出的好“的话删除了。剩下的话又变成了“人大委员长万里将回国主持人大紧急常委会，取消戒严令”，这条消息一广播，天安门广场上学生一片欢乐，大家沉浸在一种虚假的胜利之中。 <br />
万里在５月３１日回国，到上海时就下了飞机并发表了支持戒严法的声明。天安门广场上学生情绪先是失望，继而一下子又愤怒到极点，骂万里变了立场。如果说柴玲，吾尔开希，李录推翻了全体学生撤出天安门广场的决定是寄期望于万里取消戒严法，那么为什么在万里声明支持戒严法后还坚持留在广场上呢？难道他们不怕死吗？柴玲在６４前的一次接见美国电视新闻的采访节目中有非常精彩的表现。我们看到节目播出是在大约８９年６月７日左右。节目是专题介绍学生领袖柴玲的。其中有一段对话如下。美国记者问：“现在已经戒严了，你们继续留在天安门广场上有没有危险呢？”柴玲答：“是的。我们有很大的危险。我很难过，我们的这些年青的学生们可能会为这场运动流血死去。”说到这里。柴玲居然哭了起来。美国记者又问：“那么你想不想死呢？”柴玲一遍哭一遍答：“不！我要活，我还很年轻。”这段对话说明了两个事实：1。在万里发表了支持戒严法的声明后，高自联的头头柴玲，吾尔开希，李录清清楚楚知道继续留在天安门广场上学生们可能会流血死去。 2.柴玲不想死的。看来吾尔开希，李录也是不想死的。 柴玲现在口口声声说美国电视台错误地翻译了她的话。但是美国电视节目只是把她的声音略为降低，再加上英文翻译。英文翻译的声音要比原声延迟一至二秒。所以我们能很清楚地听到她讲：“不！我要活。” <br />
<br />
　　6月１日和２日，柴玲，吾尔开希，李录等６４头头对如何阻止戒严部队进入天安门广场作了严密布置。还指定专人负责把守一些街口。这些被指定负责把守的学生们都感到很光荣，他们很有一股流血牺牲的精神。但是他们不知道的是一个巨大的阴谋开始了。89年６月２日深晚到３日凌晨，北京高自联开了最后的常委会。会上由吾尔开希重点发言。吾尔开希对大家说：“根据可靠情报，明天戒严部队要进行武装清场，一定会发生流血死人的。”吾尔开希又说“这次运动已经失败。看来我们需要的不是一场改革，我们需要的是一场革命，一场体制外的革命。我们今后所要作的是床?车奶逯啤！辈窳幔?盥家沧髁丝犊?姆⒀浴?<br />
<br />
　　李禄接下来宣布高自联指挥部成员立即撤出天安门广场。再一次发了逃命钱。逃亡的目的地是美国。吾尔开希对逃亡路径及联络方法作了细述。先南下广州，然后由港支联接应。于是在６月３日凌晨三点左右，柴玲和大部分高自联指挥部成员乘着天安门上的学生正在睡梦之时，偷偷地撤离广场，走上逃亡美国的不归路。一个姓郭的头头在临跑前，突然良心发现。他说：“学生都没撤，我们指挥部提前撤对吗？是不是可以叫学生们也一起撤？”李禄说“不行！叫学生们也一起撤我们就撤不了”柴玲说“我们提前撤是为了保护火种。”于是这最后的良心也被狗吃了。这时港支联的阴谋就完全清楚了。先由柴玲，吾尔开希，李录等人用豪言壮语把上千的学生骗在天安门广场，然后在武装清场的之前，柴玲，吾尔开希，李录等人再来个金蝉脱壳之计，逃之夭夭，使上千的学生陷入生死的困境。他们已经宣誓要床?乘?裕?愠∩涎??赖迷蕉啵?怨膊?车拇蚧髟酱螅??翘油雒拦?谋厩?翟蕉唷?<br />
<br />
　　６月３日天亮以后，高自联指挥部只留下吾尔开希和李录二人。他们留下的目的是为了不让学生们发觉高自联指挥部的头头们已经跑了。他们知道，如果学生们一旦发现头头跑了，也会跑的，就会对部队清场不抵抗。 中午１２点，北京电台与电视台开始不停顿的广播戒严指挥部的通告。吾尔开希和李录也迅速地撤离了广场。下午，戒严部队开始向天安门广场推进，一些学生与市民拚死阻止部队推进，一场流血开始了。虽然高自联指挥部的头头全跑了。但是负责阻止部队推进的学生们并不知情，他们还在忠实地执行头头的命令。而在６４死亡学生中的大部分是担任阻止部队推进的。我想如果他们知道头头已经跑了，恐怕就不会去拚死了。也许丁子霖的儿子就是这么死的。<br />
<br />
　　谁也不知道第一个死去的学生是怎么死的？谁也不知道第一个被学生和市民打死的士兵是怎么死的？有很多种说法。其中有一个说法似乎比较像真的。 故事说：正当学生与戒严部队士兵僵持时，突然响起了一下枪声。于是群众叫了起来“解放军打死人了！解放军打死人了！”接着十几个身强力壮的男人向士兵猛扑了过去，抓住了士兵们的冲锋枪。士兵们吓得丢下枪跑了。但是有一个士兵却给市民打死了。脑袋被砸碎了。<br />
<br />
　　还有其他很多version的故事。但是所有的故事有个共同点：当市民扑向那个被打死的士兵时，他没有用手里的冲锋枪扫射扑向他的市民。如果他扫了，那些扑向他的市民个个都得打死，他就会活下来。为什么他不开枪呢？一是恐怕他不忍向老百姓开枪，刚才那一枪也不是他打的，二是恐怕他没有收到开枪的命令。<br />
很多研究６４历史的文献都指出，戒严指挥部一开始在使用武力这一点上是很犹豫的。士兵在向天安门推进时，主要是用士兵的躯体，枪虽带着，但是并没有使用。所以几个小时也无法打开通道。那天，还有一个非常奇怪的事。?６月３日上午，一辆挂着军用牌照的吉普，开的飞快，向设置路障的群众冲去，然后再迅速倒车，又飞快的开走。等群众叫“解放军开车压死人了！”时，吉普车已经不知去向。这到底是谁干的？目的是什么呢？很多人都指责戒严指挥部想用开车压死人来挑起群众的反抗情绪，以便镇压。但是，我的看法是：这恐怕是港支联或境外特务化钱雇人干的。在北京搞一个军用牌照和一套军装还不是轻而易举的？至于目的更是显而易见的。<br />
<br />
　　再回到打死士兵的事。那些市民说他们不知道是谁用砖头砸死了士兵。显然有人在混乱中下了毒手。再和吉普车压死人的是联系在一起，可以清楚的看到一个邪恶的力量在活动，它要挑起大对抗，大流血。在戒严士兵被打死了一个小时以后，戒严指挥部毅然下达了开枪的命令。一批戒严士兵又来到木樨地，他们命令学生与市民撤除路障，但是学生与市民拒绝服从。于是士兵就向路障开枪。有几个学生与市民立即倒下，被别人马上送去医院，路障就打开了。这个血戒一开就很难收住。有人指责戒严指挥部用士兵的死挑起其他士兵的恐怖情绪。我看，这正是那个混在市民中用砖砸死士兵的人的目的。<br />
<br />
　　士兵被打死和戒严士兵用冲锋枪开路打死学生的的消息很快传遍天安门广场，当他们要向柴总指挥请示怎么办时，发现总指挥部是空的而总指挥部外面聚集了许多学生。有个外地学生头头说他找了一个下午也没找到一个高自联头头。高自联头头丢下学生先逃跑的消息使天安门广场上的学生们陷入一片混乱。怎么办？<br />
<br />
　　大家都不知道怎么办。于是大家就去问候德建怎么办。候德建就这样当上了天安门广场学生运动最后的临时总指挥。候德建说：“现在天安门已经被包围了，逃是无法逃了根据我们对付戒严法的经验，我提三点建议：1)大家不要走动，统统坐下。2)大家不要讲话，更不能喊口号。3)大家千万不要向士兵扔东西。”他说，这样做，共产党士兵就不会向老百姓开枪，我想大陆士兵也不会的。虽然有人说这是投降，但是候德建的三不主义还是为天安门广场的多数学生所接受。天安门广场上的学生运动最终恢复了理性的思维。等戒严部队开进天安门广场，只见天安门广场上千学生黑压压一片片坐在地上（往往几百人一片）没有一点走动，也没有一点声音。戒严士兵就把学生门分片包围起来。有一个女学生说。当时叫我们女学生坐在外圈，理由是怕男学生与士兵冲突。当士兵拿了冲锋枪把我们包围时，我怕死了。后来，听到‘预备’的命令。我面前的士兵都把枪举了起来。接下来就听到震耳欲聋的枪声。我就马上趴在地上。心里直叫唤‘不要打我！不要打我！’等枪声停了下来，我知道我还活着。看看旁边的女学生也活着，但脸色苍白抬起头看看后面的男生们也个个活着。<br />
再看看前面的戒严士兵，有几个竟然咧着嘴笑。这我才知道是开枪吓我们的。但是我心里只有一个念头：‘回家去！’过了一会儿，戒严指挥车的喇叭叫高自联的头头走出来，连续叫了几个人的名字，包括柴玲王丹刘刚等人。当时没有人走出来。再过了一会儿，有一个人向戒严指挥车走去，一边走，一边叫：“我是候德建！”候德建后来对人说戒严指挥问他高自联的头头那里去了，几点走的，走那里去了等问题。候德建向戒严指挥说。他以学生的临时指挥身份表示无条件接受戒严法并要求准许学生撤出天安门广场。再过了一会儿，戒严车喇叭又叫了：“凡是能走动的学生排好队，从东南方向撤出天安门广场“　东南方向的戒严士兵让开一个小的通道还有便衣拿着照片观察走出的学生们。显然是要抓高自联的头头们。起先秩序还可以，但是很快由于大家要抢先撤出，队伍就混乱了一片乱糟糟，争先抢后，许多人的鞋子也挤丢了。也不敢拣，怕被后面拥上来的人踩死。这就是天安门运动的最后一幕。<br />
<br />
戒严部队在天安门广场清场时有没有打死学生呢？候德建说没有。很多在场的学生说也没有。但是伤是有的。特别是纪念碑旁边的学生。这是由于部队用了开花子弹，子弹打在纪念碑上，散成小的细粒，又打在学生的身上。有人的腿就给弹粒打跛了。但是当时戒严部队的确是朝天开枪。柴玲在美国说天安门广场血流成河。你早就逃走了，你怎么知道血流成河的？你们与港支联精心设计了一个可能会血流成河的死亡陷阱，但是它被候德建的三不主义破解了。当你柴玲在３日凌晨三点逃离广场时，你不是拒绝叫学生撤出吗？那时要是学生们撤出了广场，路障也不需要了，守卫路障的几百市民也 不会死了。什么叫保护火种？你的命比这上千的学生的命还重要吗？ <br />
