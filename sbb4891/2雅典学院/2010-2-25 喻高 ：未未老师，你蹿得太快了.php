													<h2>喻高 ：未未老师，你蹿得太快了</h2>
						
						
																											<div id="postmessage_492432" class="t_msgfont">22日凌晨发生的恶性强拆，我直到今天凌晨才有时间写一篇自己的博客，把事实经过真实地呈现。我根本不想把这场噩梦一遍遍在媒体上说，一直在推脱。但奇怪的是，一些和这件事没关系的人，怎么跳得这么快？ <br />
<br />
艾未未老师，你是姐姐和小东的好友，我也免不了对你有几分敬畏。但这次我真的很鄙视你，你太有政治头脑了，蹿得比狗还快。 <br />
<br />
我是那天的受害者之一，我很义愤，我们比任何人都有资格去游行。但我遵守一个规矩，在正阳，大的行动一定要半数通过才能进行。不是说我被打被抢，别的艺术家就必须听我的，陪我去长安街。如果那天换另外六个我们的艺术家，正阳一样能保得住。政府在当天早晨就有明确的态度，一定要把此事一查到底。哪怕一些人不信，也不应该毁了这个对话平台。我们在严寒中苦苦坚守三个月，难道不是为了给大家解决问题吗？ <br />
<br />
我听说你当天下午到正阳，鼓动大家去游行，没有人理你，你就开骂：“都60年了，你们还相信政府。你们应该对着镜子叫三声傻逼。”于是去了008，带着大家去长安街，在旁边拍照。让别人说不清你是参加游行的，还是工作人员，还是路过拍照的。境外媒体可以说你是领袖，说是你组织了一场民主运动，保住了艺术区。如果警察当场抓人你又可以随时溜走。今天你还托人给我们挨个打电话，说如果警察问起来，要我们千万不要提艾未未。 <br />
<br />
刘懿和吴玉仁在正阳被打伤，我对他们做的一切事情都可以理解。他们冲动的时候，您作为一个成功的长辈应该劝阻他们，而不是借他们被打，你去长安街作秀。面对那么多持刀持棍的歹徒，你未必比我勇敢。去长安街算什么？不还是要让政府解决问题吗？是让中国政府还是美国政府？你巴不得让这件事升级，普通的合同欺诈和拆迁维权太小了，要搞成一场轰轰烈烈的民主运动，象“**”一样。 <br />
<br />
就凭我是这件事的当事者，今天我必须骂你。你是个汉奸，你太厚颜无耻了。这件事一旦升级，性质变了，很多人要为你当炮灰，所有艺术家的赔偿都拿不到。你不经过正阳任何艺术家的允许就去游行，媒体会认为你是神，你代表大家维权。你是在出卖那些比你小二三十岁的艺术家，你对得起他们对你的尊敬吗？ <br />
<br />
你给“暖冬”的钱我们如数退还。我们就事论事，做维权展是要解决具体问题。我们做不到你期望的那么大。“暖冬”第三站运动会那天，我不赞成你来现场的，但别人请了你我也不好说什么，你那天对媒体说的一些话是非常不合适的。 <br />
<br />
有人巴不得借维权来结党，巴不得来挑事。昨天有人在网上散布，说警察来抓吴玉仁刘懿，要我们集体去008支援。今天又有人散布说吴玉仁烧了警车，明天不知道又要散布什么，反正目的就是要让大家闹事，把事越做越大，而英雄的那几把交椅都排好座次了。我真的要说一句：阿仁，刘懿，你们打架的时候非常英勇，不是见风使舵的人，你们已经是英雄。上了长安街，反而什么都不是，那是吸引媒体作秀的地方，是否被媒体围住的感觉很飘飘然？ <br />
<br />
艺术界的精英意识和权威意识是任何行业里最重的，“精英”就是“强权”的变异，居高临下对别人宣扬平等，这太滑稽了。我倒是欣赏西方人讲的自由，所以不和任何人结党结社，和谁都不捆在一起，才是做人的尊严。打着任何真理的名义结党，混好了也就是个邪教教主。你已经够成功了，不要到处侵略别人。 <br />
<br />
希望关注这件事的朋友不要被一些媒体和传言误导，坚守了三个月的艺术家们有权过上正常人的日子，我们有权保护私有财产，有权去度假，有权拥有规律的作息和充足的睡眠，有权不被骚扰。至于这个维权英雄，谁爱当谁当，这就是一张小丑的面具。 <br />
<br />
我很少骂人，也从不匿名骂人。如果得罪了艾未未的粉丝，你们也可以来骂我，匿名真名我都不怕。来了拆迁机器我还可以挡，来了暴徒我还会周旋，来了“神”我不会被神洗脑。 <br />
