													<h2>熊飞骏：对中华文明伤害最大的知识精英</h2>
						
						
																											<div id="postmessage_670691" class="t_msgfont">中国拥有一个独特的知识分子阶层。<br />
&nbsp; &nbsp;知识分子的使命应该是推动社会进步的，西方知识分子就曾很好地履行了这一职能。<br />
&nbsp; &nbsp;和西方知识分子不同，中国知识分子在历史上很少扮演推动社会进步的角色。<br />
&nbsp; &nbsp;为什么会这样呢？<br />
&nbsp; &nbsp;因为中国传统知识分子是在奴化教育下长成长起来的读书人，学的多是是反文明反人性的垃圾知识，培养的是阻碍进步的奴性思维。拥有垃圾知识和奴性思维的人自然很难承担推动社会进步的使命。<br />
&nbsp; &nbsp;在中国漫长的历史文化长河中，下面三个知识精英对中华文明的伤害无与伦比。<br />
&nbsp; &nbsp;他们是叔孙通、董仲舒、朱熹。<br />
&nbsp; &nbsp;叔孙通造就了统治者的无限特权意识，使统治者自绝于人民，最高统治者自此成了货真价实的孤家寡人。<br />
&nbsp; &nbsp;董仲舒是中国思想专制的总设计师。<br />
&nbsp; &nbsp;朱熹则扼杀了儒家思想残存的那点活力成分，是虚伪和分裂人格的始祖。<br />
&nbsp; &nbsp;首先说叔孙通其人。<br />
&nbsp; &nbsp;刘邦得天下之初，帝王的架子并不大，经常和手下的文臣武将打成一片。部下在皇帝面前也没多少顾忌拘束，不少黑社会朋友当着皇帝老哥的面开怀畅饮，大声喧哗，然后乘着酒兴展示自己的超人剑术，动不动就把大厅的廊柱给吹断了。害得皇帝老哥拔腿往大门方向跑，和部下争路逃命。那时虽然也有人拍皇帝的马屁，但当皇帝面说难听话甚至于公开蔑视帝王权威者也大有人在；且这些人并没有因此获罪。如刘邦喜欢找韩信侃大山，有次自我感觉很好，很自信地问韩信自己可以统领多少兵马？韩信用轻蔑的眼神溜了皇帝一眼，然后回答说“陛下最多能率十万兵”。刘邦接着问韩信同样的问题，韩信居然回答说“臣是多多益善”……这等于是当面嘲讽皇帝不会带兵，宣称自已比皇帝高明。对于韩信吃错了药般的狂言，刘邦虽然心底掠过一丝不快，但只是一笑置之，以后照常找韩信侃大山如故。<br />
&nbsp; &nbsp;因为能经常从部下口中听到难听的真话，刘邦很少患帝王的通病――“自以为是的大头症”，对自己的认识也很客观。对于他能扫灭群雄问鼎帝王宝座这样的伟大事业，刘邦就从没归功于自己能力过人，甚至于认为自己的能力和作用还不如他的三个部下韩信、张良、萧何？韩信的“多多益善”虽然泼了刘邦的面子，但却有利于刘邦直面自己的弱点，避免在自我膨胀之时劳命伤财瞎折腾，或死要面子活受罪。刘邦在白登山突围后，能承认自己的力量不如匈奴，明智地采取“和亲”的方式避敌锋芒休养生息，就是得益于韩信等人的难听真话。如果此前刘邦只能听到动听的好话，在强大匈奴面前就有可能象隋炀帝征辽东一样飞蛾扑火，落个国破家亡的惨剧。<br />
&nbsp; &nbsp;刘邦以前的历代帝王也能很容易放下架子，最起码能在“人才”面前平易近人，甚至于动不动就强留那些能力超人的杰才俊士同床睡觉。秦孝公赢渠梁就和公孙鞅同吃同睡三天讨论变法大计；始皇帝赢政碰上尉缭时也喜欢得长跪在前请教统一韬略，当得知尉缭不辞而别时竟然丢下手中的饭碗拔腿就去追，为挽留尉缭不惜赌咒发誓要与对方共甘苦同富贵，不惜放下帝王的尊严倒过来装孙子……<br />
&nbsp; &nbsp;是叔孙通鼓动帝王不顾老命端架子，让帝王普遍患上了自我膨胀的大头症。<br />
&nbsp; &nbsp;叔孙通是一个百无一用的儒生，在秦末群雄混战年代，带着另一邦小儒生象老鼠一样四处逃命，为了活命连碰上阿Q那样的混混也能跪在地上装孙子，因此得以活到刘邦当皇帝的那年月。<br />
&nbsp; &nbsp;在战争年代只知抱头鼠窜的孬种，在和平时代却可能混到人上人。大凡抱头鼠窜的胆小鬼都善长阿谀奉迎的伎俩，而和平年代的统治者对阿谀奉迎常常有特别的爱好。叔孙通就正好属这号人。刘邦刀枪入库马放南山之时，叔孙通发现自己无耻的机会来了，就利用装孙子的本领骗过了道道门房，得以混到刘邦面前当面拍帝王的马屁。他假装成忠言逆耳的模样，提醒刘邦说他的国务办公大数虽然巍峨雄伟，但总象缺少点什么。如果能在皇帝的办公地点注入“朝觐礼仪”，不但能大大地锦上添花，还能大大抬高天朝大国的对外形象。<br />
&nbsp; &nbsp;叔孙通的拍马技术炉火纯青，刘邦不可能不上套，竟然答应把文武大臣全交给叔孙通去教导“朝瑾大礼”，但嘱咐他不要搞得太繁琐，简单务实最重要。<br />
&nbsp; &nbsp;叔孙通让文武大臣受了三个月洋罪，终于可以让皇帝检阅一下“朝瑾大礼”是什么东东了。<br />
&nbsp; &nbsp;那是中国历史上具有里程碑意义的一天。刘邦在宫女太监引导下上了一辆装饰豪华的黄金御辇，在俊男靓女的牵引下向“国务办公楼”进发。他看到衣着鲜艳的卫兵分列在大道的两边，一个又一个的传令兵大声向后报告皇帝来临的消息。办公楼大门两厢的卫兵象雁翅一样摆开，五光十色的彩灯把楼前的广场映照得通明透亮。到达办公楼大门口时，早就站在那里等候的有身份太监上前搀扶皇帝下车，传令兵用更嘹亮的声音向内报告皇帝到来的消息。先前还在交头接耳的大臣们霎时鸦雀无声，脸部表情即刻呈恭敬肃穆状。刘邦在太监搀扶下来到办公大厅后，看到文武大臣分列两厢，那些平时习惯高声大嗓呦五喝六举止狂放的黑社会哥门一个个低眉顺眼，大气也不敢出，唯恐自己的身体站得不够直，表情不够恭敬。刘邦在特制的龙椅上坐定后，太监宣布开始办公，大臣依次上前捧着秘书们精心撰写的发言稿，用最恭顺最精练的语言向皇帝报告工作，三言两语后就回到自己的位置继续庄严肃立。然后是皇帝本人拖长声音作报告，文武大臣认真听的样子象是唯恐遗漏一个字。最后由监察官站出来控告张三没站样李四在偷笑，并宣布对失礼者的惩罚措施……<br />
&nbsp; &nbsp;朝仪结束后，刘邦通体舒坦，从灵魂深处发出大声感叹：<br />
&nbsp; &nbsp;“天老爷！我今天总算知道当皇帝的威风了！”<br />
&nbsp; &nbsp;然后是重赏叔孙通，把他提升为大官。叔孙通又把他的徒子徒孙提升为小官，派他们去全国各地去向地方统治者传授大力提升统治者威严其实是在孤立他们的公务马屁术，把统治者与下属民众有效地隔离开来。久处穷困而又绝望环境中的那些小儒生自此时来运转，找到了施展马屁特技的后台，禁不住大声歌颂他们的老师：<br />
&nbsp; &nbsp;“叔孙通真是圣人！”<br />
&nbsp; &nbsp;从此，国家元首不但跟人民，就连最尊贵的大臣，也都被叔孙通最邪恶的杰作“朝仪”，隔开一段距离。战国时代那种君臣间面面坐立，膝盖碰着膝盖长谈的时代在中国自此一去不返，专制政体遂走进一条自我封闭永不回头的死胡同。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;董仲舒的故事我就不细讲了。他的最大杰作就是建议汉武帝刘彻“罢黜百家，独尊儒术”，把中华民族思想流动的长河拦腰斩断。<br />
&nbsp; &nbsp;董仲舒是一位专门研究《五经》之一《春秋》的博士。他在参加国家人才选择考试的的万言试卷中攻击其他学派全是左道旁门，邪说妖言。建议：“凡是不在《五经》之内的著作，以及非孔老二所传授的书籍，应一律禁绝，不准流传。”<br />
&nbsp; &nbsp;董仲舒的邪恶建议被拥有浓厚专制思想的皇帝刘彻采纳，独尊儒术的思想控制自此成了国家的大政方针。一个酝酿历史大倒退的的巨变在董仲舒的策划下不声不响地发生了。<br />
&nbsp; &nbsp;董仲舒把儒家思想提到天皇至尊的地位。祭祀部所设的“博士”官职原来由各学派人士分别担任。此后只有儒家学派才能充当，而且限制范围，只能研究《五经》。其他学派人士全被驱逐，儒家遂独霸学术中枢定于一尊。其他学派的著作全被政府长期视为“邪说”、“妖言”禁止阅读研究，遂逐渐从知识分子脑海中消失。诸子百家只剩下一家，一家中只剩下《五经》。儒家思想自此成为皇帝钦定，中国唯一且排他性的正统思想。<br />
&nbsp; &nbsp;董仲舒还开了中国奴化教育的先河。儒家学派在祭祀部之下，创办国立大学（太学），由博士担任教师，传授《五经》和孔老二的学术思想，用儒家思想对学生进行全面彻底的洗脑。学生毕业即被任命担任地方政府的官员。太学不但是平民进入政府的唯一途径，因而增加知识分子对儒家学派的向心力；而且长久积累下来，儒家学派布满了各级政府，成为一种排他性极强的儒家系统。<br />
&nbsp; &nbsp; 董仲舒以前的中国是光芒万丈的思想学术自由时代。自董仲舒设计的的邪恶思想学术体制出台后，中国的思想学术开始夕阳西下，代之而起的是思想专制时代。这个在春秋战国时代产生过八位杰出思想家的伟大国度，在此后的两千年思想专制时代没有诞生过哪怕是一位思想家。<br />
&nbsp; &nbsp;思想是一个民族文明进步的灯塔，没有思想的民族只能在漫漫长夜里摸石头过河，在付出巨大惨重的代价后才发现自己又回到了当初的起点，甚至连起点都不如。<br />
&nbsp; &nbsp;朱熹是中国狭隘、虚伪、极端民族劣根性的祖师爷和思想专制的集大成者。<br />
&nbsp; &nbsp;儒家思想的核心虽然是保守崇古，但多少残存点活力成分，比如对暴君的无关痛痒谴责和民众抗暴的同情。可是就连那点可怜的活力成分也让朱熹给毁掉了。<br />
&nbsp; &nbsp; 朱熹是道学家的始祖。他把“道德”提升到至高无上的地位。朱熹的“道德”尺度并非“勤奋、诚信、正直、善良、进取、包容、平等”等有教养的国民必须具备的文明修养；而是一套反人性的扭曲价值体系。在朱熹眼中，一个道德上没有瑕疵的人才有资格去当官治国；一个道德上有暇疵的人即令再有才干也不配当官干公务。朱熹的“道德标准”是简单的二分法，人类只有两种：“不是圣贤，便是禽兽。”圣人是一种国家社会的模范公民，是没有任何弱点的“伟光正”，主要功能在维持社会秩序的安定，维持既定的名分和尊卑，使不作任何改变，以免名分和尊卑紊乱。这种道德法则的精神和形态被称为“礼教”。使礼教达到至善的关键在于“存天理灭人欲”。一个人必须努力减少自己的欲望；减少欲望最有效的方法是无时无刻不在思念圣人们所下定义的道德，无时无刻不在思念忠君爱国，除此之外别无他念。就算心里不那么想表面上也要装得煞有介事一本正经。<br />
&nbsp; &nbsp; 朱熹的道学设计也许有善意的一面，可在现实社会却不容易起指导作用。因为道学家的要求太高，所以很难有人能够达到标准，连道学领袖朱熹都不能。朱熹担任提举浙东刑狱期间，跟台州州长唐仲友为了争夺一位秀外惠中的妓女严蕊失败，就曾恼羞成怒向皇帝上奏章弹劾，公报私仇揭发唐仲友有作风问题。本人在学生时代就曾读过严蕊为此作的一首词《卜算子》。<br />
&nbsp; &nbsp;不是爱风尘，似被前缘误。花落花开自有时，总赖东君主。<br />
&nbsp; &nbsp;　　去也终须去，住也如何住？若得山花插满头，莫问奴归处。<br />
&nbsp; &nbsp;朱熹在感情激动时都不能实践他对人的要求，就更不用说充满七情六欲的芸芸众生了。朱熹的道学标准扭曲了中国人的灵魂，在国民灵魂深层播下了虚伪变态的种子。<br />
&nbsp; &nbsp;道学家在礼教甲胄之中，因不能忍受那种压力，往往人格分裂，成为言行不符，甚至言行恰恰相反的两面人，道学家所要求的道德，几乎全部都是外销品。中国人“狭隘虚伪”的劣根性自此在灵魂深层深深扎下了根。<br />
 因为自己做不到，所以道学家往往一味要求别人在礼教甲胄中受苦。这种心理背景，产生一种愿望，对别人的责备永无止境。动不动就要求别人为了忠君爱国为了维护名节去死，甚至即令死也不能逃脱责备。此习性最容易培养国民的冷血变态品格。<br />
&nbsp; &nbsp; 在这个没有任何人情味的道学思想指导原则下，寡妇被迫活活饿死也不许再嫁。音乐、戏剧、绘画一律成为坏人心术的毒品。作诗也属于堕落行为。其他文学作品如小说、散文之类看一眼都是罪过。只有低贱的人才去从事体力劳动，。圣人则必须全神贯注于“道德”和做官。人们连走路都要有一定姿态，奔跑和急促的步子，都是轻浮下贱。服从传统权威，崇拜古人古事，崇拜祖先都是最高贵的善。任何改变和抗拒的念头都是邪恶，必须在念头一起时就予以无情克制。任何发明创造，更都是专门取悦小人和女人的奇技淫巧。<br />
&nbsp; &nbsp;…………<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;本人在七前年写的《知识分子的悲哀》一文中曾给中国传统小知识分子画过一幅像，现在转贴到这里也许仍有部分现实意义。<br />
&nbsp; &nbsp; 自视过高，互不买帐，好口舌之争，为一些鸡毛蒜皮的小事也要争个是非曲直。<br />
&nbsp; &nbsp;　　过分以自我为中心，不愿正视他人的优势和长处；办事分不清主次，总喜欢在细枝末节的问题上自我表现。<br />
&nbsp; &nbsp;　　没有全局观念，不善在协同事业中做出必要的让步；在宽松的环境中过分吹毛求疵，在淫威和暴力的压制下又能出奇地忍辱负重。<br />
&nbsp; &nbsp;　　对自己的不幸记忆深刻，对人类的痛苦则引不起深刻的同情。<br />
&nbsp; &nbsp;　　好抱怨不好行动，台下勇士台上懦夫。<br />
&nbsp; &nbsp;　　对个人的安危进退过于在乎，缺乏为共同事业所必需的牺牲精神，结果付出的代价更大。<br />
&nbsp; &nbsp;　　思想固执，小事精明大事糊涂；对他人求全责备，对自己遍设台阶，很少在口头上肯定他人的能力。<br />
&nbsp; &nbsp;　　行为懒散，作风疲沓，缺乏进取精神；自以为掌握知识就可坐享其成，丝毫意识不到知识与贡献是绝然不同的两个概念，报酬只能以贡献来衡量。”</div>

							
							
							
							
