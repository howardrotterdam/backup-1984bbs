													<h2>那些年轻人为什么忧伤？</h2>
						
						
																											<div id="postmessage_466150" class="t_msgfont"><a href="http://www.dfdaily.com/node2/node24/node619/userobject1ai209541.shtml" target="_blank">http://www.dfdaily.com/node2/nod ... ject1ai209541.shtml</a><br />
<br />
　夏佑至 <br />
<br />
　　<br />
<br />
　　正在网上疯传的《网瘾战争》描述的远非一场战争，这部突然走红的视频短片充其量是一声叹息，连抱怨都谈不上。尽管“看你妹”在结尾部分的演说慷慨激昂，号召魔兽玩家们“请举起你的手”，但现实却远不是视频里想象的结局。没人做出任何姿态响应这段演说，倒是到处都可以听到不是魔兽玩家的人连呼“内牛满面”。<br />
<br />
　　魔兽玩家的数量――据说有500万之多――和这个群体的忍耐程度似乎成正比。他们自称对这款游戏极为痴迷，依靠它忘记生活中一切不幸、不平和不堪回首的事情，但是在沦为行政部门之间利益纠纷的牺牲品时，却没有一点像样的抗议。公民抗议并不是多么稀罕的事情。厦门的人口总数不超过300万，广州番禺区也差不多，对政府说“不”的人又只是各自总人口中极小的一部分，但抗议者用鲜明的观点和有力合法的行动，维护了自己的权益，堪称公民的典范。<br />
<br />
　　这说明行动能够改变现状，也只有行动，才能够改变现状。对比一下魔兽玩家的表现，可以非常清楚地看出两个群体之间的差别。也许魔兽甚至所有的网络游戏对玩家来说并不像他们自己想象的那样重要，“看你妹”滔滔不绝的演说只是夸大其辞；要么就只能说明这个群体的行动力是极其低下的。<br />
<br />
　　语言能够促成行动，也可以变成一种听上去漂亮、实际很空洞的东西。说到底，这是一种文字游戏。《网瘾战争》和《一个馒头引发的血案》没有什么分别，但胡戈是一个人，他单枪匹马去挑逗陈凯歌，比号召500万玩家“举起你的手”的“看你妹”靠谱多了。<br />
<br />
　　《网瘾战争》具备很多刺激眼球的元素，却满足于带给受众一种廉价的安慰，不禁让我产生了一种联想。曾经有个年轻的作家写过一本主题含糊的书，书名叫《那些忧伤的年轻人》，前不久，有一家周刊拿这个书名作了封面报道的标题，内文中报道了许许多多互联网公司因政策收缩而受重创。这些公司的创始人无不聪明而勤奋，却被突如其来的打击搞得狼狈不堪，他们的第一反应是调整自己的姿态，从斗志昂扬的创业者变成了忧伤的年轻人。<br />
<br />
　　我们都经历过这样的时候：怀着忧伤的情绪，噘着嘴巴，满腹委屈地站在黑板前面，等待下课铃响。那些忧伤的年轻人现在就在被罚站。他们并非不了解什么是“政策收缩”，只是选择了忍受。这和聪明的魔兽玩家如出一辙。<br />
<br />
　　所谓的政策收缩，提醒了我们这样一个事实：经过30年改革开放，我们付出了沉重的代价（污染、工人失业、农民失地、贫富分化等等，更不要说因为事故、自残式的抗议和公权力被滥用而造成的死伤了），但自由市场的目标还很遥远。放眼世界，但凡互联网经济发达的国家，推动行业发展的最大力量，毫无例外是那些有创造力的头脑。政府要做的是尽力为他们提供服务，而不是忙于决定那些聪明的头脑的命运。<br />
<br />
　　这个局面已经把中国的互联网业推到了一个十字路口。这不是选择百度还是谷歌的问题，而是如何对待21世纪里最重要的产业，如何面对这个产业发展规律的问题。如果中国不想一个十年接一个十年地生产衬衫、皮鞋和玩具，如果结构调整、产业升级这些说法不是磨嘴皮子玩，就必须面对这个现实：周期性和不定期的政策收缩正在扼杀互联网的生机。<br />
<br />
　　这个行业高度依赖创造性思维，依赖网上协作，所以信息的自由流通至关重要。将遥远的和陌生的人们联系起来，是互联网经济的基础，是未来服务业的核心。淘宝、QQ、豆瓣，甚至百度吧，都给中国人提供了前所未有的自由空间；人们可以根据兴趣和爱好，找到心仪的商品，或者一群志趣相投的人，并因此成就了一批成功的互联网公司。没有信息流通的自由，马云、马化腾、杨勃和李彦宏就不可能获得成功；如果中国不能找到一个稳定而自由的市场规则，他们的成功就不可持续、不可复制。<br />
<br />
　　不幸的是，事情正朝糟糕的局面发展。看到那些忧伤的年轻人宅在家里，把才华浪费在《网瘾战争》上，真是一件可叹的事情。<br />
<br />
　　（作者系媒体人士）</div>

							
							
							
							
