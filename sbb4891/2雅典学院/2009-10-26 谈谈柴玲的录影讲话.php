<br />
　　　　　　　　　　　　　谈谈柴玲的录影讲话<br />
<br />
<br />
　　1989年6月4日，在中国结束专制统治之前，是永久的话题。笔者称之为“天安门民主运动”。49年城头变换大王旗之后，天安门广场有过多次上百万人的集会。但真正发自人民的声音，之前只有1976年那次。而这次，更广泛，更深刻，称之为中国民主进程的起点，当之无愧。<br />
<br />
　　这五十多天，至今只有一部完整的记录影片（另有一部《移山》，未见流行，而从看到的介绍，也不足为论）。就是这部《天安门》，从面世之日起，就卷入了一场无休止的争议之中。时至二十年后的2009年，又传来为该片而引发诉讼的消息，双方均在互联网上发布公开信，呼吁签名支持。争议基本上围绕着柴玲1989年5月28日对美国记者金培力（Philip Cunningham）的录影讲话。<br />
<br />
　　在国内关注时政的论坛上，也频见对此的争议。既然把这次运动称之为中国民主进程起点，对此中如此具争议的事情，不应回避。故此，先就“柴玲1989年5月28日对美国记者金培力（Philip Cunningham）录影讲话”谈谈个人看法。望诸朋友不吝赐教，但希望不要出现人身攻击，也不希望有人搅帖。<br />
<br />
　　讨论要求：对话的目的是寻求真理，不是为了斗争；不做人身攻击；保持主题；辩论时要用证据；要分清对话与只准自己讲话的区别；尽量理解对方的意思。――1984BBS版规<br />
<br />
　　对《天安门》指责最多的，是“断章取义”地剪辑柴玲讲话。为此，下文【】内所引柴玲的话，均出自封从德提供版本，小标题为封从德所加。顺便说，卡玛提供的版本并无大出入。任何评论，免不了截文取义，是否“断章”，只能仁智各见了。为看官方便判断，封从德版全文另帖发布。<br />
<br />
<br />
第一，“流血”<br />
<br />
　　先来看事实（按时间顺序）：<br />
<br />
【二・绝食<br />
<br />
　　……当时我在北大，我说我自荐为北大绝食团的召集人，因为我有一种责任感，我觉得北大的绝食我的演讲起了很大的作用，我想是这样不是居功，我觉得我必须为这四百多个孩子的生命负责。<br />
<br />
三・成立指挥部<br />
<br />
　　……我说我自己愿意做这个绝食团的总指挥吧，好像是，不记得怎麽说的了。而参加这个绝食团唯一的资格就是在同学们牺牲之前你愿意首先牺牲自己来使更多的同学们活下去。<br />
<br />
四・最後的阵地<br />
<br />
　　同学们老在问，我们下一步要干什麽，我们能达到什麽要求，我心里觉得很悲哀，我没办法告诉他们，其实我们期待的就是，就是流血。就是让政府最後，无赖至极的时候它用屠刀来对着它的，它的公民。我想，也只有广场血流成河的时候，全中国的人民才能真正擦亮眼睛。（哭）他们真正才能团结起来。但是这种话怎麽能跟同学们说？<br />
<br />
　　所以我觉得很悲哀。（抽泣）这些话没有办法直接跟同学讲，跟同学说，我们就是要在这里流血，用我们的鲜血和生命来唤起民众，同学们肯定会这样做的，但是，他们还是年轻的孩子们（哭）。<br />
<br />
五・悲哀<br />
<br />
　　……（哭）我感觉到我那麽热爱广场上这几万万的孩子们。（哭）我有责任用我们的生命坚持到底……<br />
<br />
　　我知道我这时候要宣布我辞职，或者我引退，同学不会理解，会骂你，我不在乎，整个运动我挨了很多骂，（哭）我很淡然。<br />
<br />
八・下一步<br />
<br />
　　下一步作为我个人，我愿意求生下去。广场上的同学，我想只能是坚持到底，等待政府狗急跳墙的时候血洗。<br />
<br />
　　……二十五号那天，……我说政治犯判多少年？他说以前判三年，後来判五年，现在加到七年，後来加到十七年。当时我很悲哀，我在想十七年以後我出来就四十岁了，很不甘心的呀。<br />
<br />
梁淑英问：你自己会继续在广场坚持吗？<br />
<br />
　　我想我不会的。<br />
<br />
　　因为我跟大家不一样。我是上了黑名单的人。被这样的政府残害，不甘心，我要求生。我就这样想。我不知道会不会有人说自私什麽的，但是我觉得，我的这些工作，应该有人来接着干下去，因为这种民主运动不是一个人能干成的。<br />
<br />
　　这段话先不要披露，好吗？】<br />
<br />
　　不用“断章取义”，也无需分析猜测，柴玲自己说得非常清楚，而且是前后多次表达。我只简单综合在一起，就是：<br />
<br />
　　“只有广场血流成河的时候，全中国的人民才能真正擦亮眼睛”，“用我们的鲜血和生命来唤起民众”，“广场上的同学，我想只能是坚持到底，等待政府狗急跳墙的时候血洗”；<br />
<br />
　　“我这时候要宣布我辞职，或者我引退”，“作为我个人，我愿意求生下去”，“因为我跟大家不一样。我是上了黑名单的人”；而且，“这段话先不要披露，好吗？”。<br />
<br />
　　不错，柴玲最后没有辞职或引退，而且坚持到了广场最后一刻。这后面的行为不能否定她前面自己的表达，前面是她主动的表达，想来也不可能是受到什么压力而违心所言――没有这样违心的合理动机。之所以为什么又有后面的行为，可以有很多合乎逻辑的解释，比如：后续安排尚未完成，某些技术程序尚在实施中，或有其他突发事情出现……没有人准确知道63夜就是最后一刻。另外，也有个细节可以参考：28日之后，《民主歌声献中华》组织者之一香港导演岑建勋携数千港元善款到京。顺便说，原定撤回校园的决定在30日被柴玲推翻。这些都只是罗列现象，没有更多具体的材料，无法最后定论。<br />
<br />
　　同时，柴玲在讲话第二天向记者说：<br />
<br />
　　【我提出辞呈后，听到方方面面的意见，他们认为，我现在提出辞职不太合适。理由是因为我已经在同学中有相当的威信，而且在这个大家军心都不太稳定的时候提出辞职可能会有些影响。考虑到这个方面，我想我还是再继续、就是先留下来，同时作适当的休息，等待把新的一套体制建立起来。<br />
<br />
　　对，我有这样的考虑，因为我觉得工作的重点已经不在广场了，而应是在全国各地上。本来我有一点心愿，我想我很希望到全国各地去走一走，甚至有可能到香港或别的地方看一看，究竟外面是怎么一种局面，我要亲自去看一看，然后决定广场这个战役究竟应该打多久，有可能出现一种什么样的效果。――《天安门》纪录片】<br />
<br />
　　“等待把新的一套体制建立起来”，“到香港……亲自去看一看，然后决定广场这个战役究竟应该打多久”，这样的叙述，除了让人感觉自大和幼稚之外，是否也让人觉得：出乎常理之外的表达，会不会隐藏了“先不要披露”的内在想法？<br />
<br />
　　网上总是有人指责：不能让学生为屠杀负责。完全正确！屠杀的责任完全在当局，而不在任何一个学生领袖。反思运动，是为了将来做得更好，不是审判。借用一篇网文的标题，这是为了“回顾历史，检讨过失，提高自身，迎接未来”。<br />
<br />
　　更何况，同一个柴玲，在同一个讲话中还说过：<br />
<br />
　　“我自荐为北大绝食团的召集人，因为我有一种责任感，……我觉得我必须为这四百多个孩子的生命负责。”“我自己愿意做这个绝食团的总指挥吧，……而参加这个绝食团唯一的资格就是在同学们牺牲之前你愿意首先牺牲自己来使更多的同学们活下去。”<br />
<br />
　　若把同一个人在两个小时内前后说的话对照起来，任何诸如：年轻，每个人都有权利求生，等等，都不足以辩护。<br />
<br />
<br />
第二，“权力”<br />
<br />
　　还是先来看事实（按时间顺序）：<br />
<br />
【一・四二二参加运动<br />
<br />
　　（擦眼泪）我的感觉就是说，参加筹委会的同学有热血的，有真诚的，也有个人目的的，有虚荣的。有的人在死亡面前是一副面孔，那麽胆怯逃避；可在虚荣面前他又是另外一副面孔，那麽功利。<br />
<br />
三・成立指挥部<br />
<br />
　　……我可以认为，市高联以前的一些领导同学首先没有领导素质，再一个，动机并不是那麽纯洁。<br />
<br />
　　……而学生中又出现什麽呢？小的，学阀割据，小学阀，自己拉起一帮人作纠察，而他纠察队长可以说我是纠察总指挥什麽的。<br />
<br />
　　……市民的热情在降下去，市民人心出现动摇，甚至有些市民开始慰问解放军了。<br />
<br />
　　更可怕的是，有些同学逐渐被政府收买，他们作一种学生中的特务和奸细。<br />
<br />
问：你们有感觉吗，就是说有学生叛徒？<br />
<br />
　　绝对有感觉，而且我们有证据。<br />
<br />
问：他们给多少钱？<br />
<br />
　　钱我不太清楚。给他们什麽样的许诺和交易不清楚。但有一次，我也有一次跟政府接触的机会。我在跟他们的谈话中感到他们带有一种强烈的暗示性。<br />
<br />
四・最後的阵地<br />
<br />
　　……我是总指挥，我一再要求这个权力，掌握这个权力，就是为了抵制这种妥协，这种投降派。而且有人作为北高联和外高联，外校的高联，他们又很愿意要这种权力。（沉默）<br />
<br />
　　作为一些知识阶层的人，成立了一个知识联、各界联合会议，爱国维宪委员会，在昨天会上我很愤慨、很愤慨，因为我感觉到这些人也是在利用这次学运重新塑造他们的形象。我一再抵制这种倾向，（断）像刘晓波把吾尔开希……对不起，上面说的可以删一下吗？要推举他为运动发言人什麽的。我在运动中对这类人有些看法，吾尔开希，就是他曾经利用他的影响，他所处的那种领袖地位，对整个学运不断产生了很大的破坏作用。<br />
<br />
　　尤其可悲的是，有一些同学，有一些什麽上层人士，什麽什麽人物名流，他们居然为了达到个人的目的，完成自己的一些交易，拼命地在做这个工作，就是帮助政府，或者不让政府采取这种措施，而在政府最终狗急跳墙之前把我们瓦解掉，分化掉，让我们撤离广场。<br />
<br />
五・悲哀<br />
<br />
　　……我从来不迷恋权力，我只是为了，为了良心，我才不愿把这个权力放弃给那一小撮那种投降派和阴谋家。但是我不知道这些人为争夺这权力，（抽泣）而发起一次一次的攻势。<br />
<br />
　　我昨天非常痛心，因为我，（哭）因为我，又一次看到形形色色的人在出卖这场运动，在葬送这场运动，我感到很痛心的就是，最初绝食的这一千多名同学，（抽泣）我可以说他们的身体健康已经造成了很大的摧残，而这些人（抽泣）要葬送这一千多名同学，还有後来成千上万的人民和同学他们用生命换来的一些成果，他们要葬送掉，就只是为了他们个人的目的，个人的希望，个人的私利（哭）（摇头）中国人……<br />
<br />
八・下一步<br />
<br />
　　三十号撤离这个决议是所谓的爱国维宪各界会议决定的。他们造成了很坏的影响，当时我也在，但是我没有很明确的意识到有这麽大的危害力。同时，兴奋点集中与他们有一些人要通过这个运动树一些自己的形象，什麽什麽的，……】<br />
<br />
　　我在很多场合都说过：任何一个潮流，站在潮头的往往是个人素质并不太好的人。我强调的是：不要因了弄潮儿的个人表现而否定整个潮流。但，尤其在大潮过后，剖析那些给潮流带来不良影响的弄潮儿，是对整个潮流反思的重要内容。非此，无以“回顾历史，检讨过失，提高自身，迎接未来”。原因很简单，你站在潮头，你对整个潮流的影响大于其他人。就像在常态社会中，人们对公众人物有着比对常人更严格的要求。<br />
<br />
　　运动伊始，就认定合作者有“个人目的”，“虚荣”、“胆怯逃避”、“功利”、“没有领导素质”，“动机并不是那麽纯洁”，继而又把难以避免的混乱现象说成是“学阀”。最后，言之凿凿地说“被政府收买，他们作一种学生中的特务和奸细”，证据呢，仅仅是自己在和政府的接触中感到的“暗示性”――至今，尚未看到任何人在回忆或任何资料中揭示出那场运动有学生特务。而如果有人掌握了证据，二十年之久，在自己已经安全的前提下，却不公布于众，怎么说？<br />
<br />
　　为了给对手泼污水，甚至罔顾事实地说“市民开始慰问解放军了”――不知所指是北京市政府组织的农民游行，还是拦截军车的群众给军人送食物，而这些都不应该理解为“慰问”。即便是屠杀之后的慰问，应该说也并不是民心所在。<br />
<br />
　　我只能说：如果再有任何形式的潮流兴起，如果我们能够享有任何形式的“民主选择权”，甚至是在比如论坛这样的场所，千万要注意不要投票给这样的人！要十分警惕，这种离心离德的思维无法与之合作，只会带来无休止的内斗。<br />
<br />
　　其实，关键在于“权力”。<br />
<br />
　　北高联、外高联、爱国维宪委员会，几乎所有运动中的领导形式都被冠以“投降派”、“阴谋家”，认定是“在出卖这场运动，在葬送这场运动”，认定他们都是“为了达到个人的目的，完成自己的一些交易，……帮助政府，或者不让政府采取这种措施，而在政府最终狗急跳墙之前把我们瓦解掉，分化掉，让我们撤离广场。”于是乎，就可以推翻集体作出的决定？<br />
<br />
　　我并不认为柴玲从一开始就“一再要求这个权力，掌握这个权力”，是为了后来的某种安排。我是想：在一个争取民主的过程中，如果以这样的思维方式参与，并以个人的认知和掌握的“权力”随意推翻“民主程序”作出的决定；那么，这样的运动走向是一定争取不到民主的。<br />
<br />
　　“权力”被许多人所迷恋，这似乎是中国传统之一。但不掌握“权力”，如何成事？这似乎是个悖论。解决的办法就是民主程序，而这正是我们所追求的。你可以要求“权力”，但你必须遵守规则；你所掌握的，只能是民主程序所赋予你的“权力”。当然，猝然间兴起的运动，加上数千年至少是半个世纪的传统和教育的熏陶，学生们缺乏民主训练，在整个运动中存在缺失和不足。这不能责怪任何人，但我们也看到许多学生领袖在尽力完善或建设民主秩序。而不负责任的无端指责，没有证据的诛心，罔顾事实的妄言，这种为了“正确目的”的“不择手段”，不正是我们的对手曾经并一贯使用的吗？何况，人家的“正确目的”还有炫目的“共产主义理想”来遮掩，你的“正确目的”只是自我标榜甚至欺骗。<br />
<br />
　　要说“回顾历史，检讨过失，提高自身，迎接未来”，对“权力”的反思，要比“流血”问题更重要。<br />
<br />
<br />
第三，“对谈”<br />
<br />
　　这里不是说学生与政府的“对话”，而是指封从德版本中的小标题“六・对谈”下的内容。有兴趣的朋友可以自去查看。<br />
<br />
　　从这段“对谈”中，一方面可以对以上两个问题有所补充验证，一方面可以看出柴玲对运动的理解，也可以看出柴玲本人的素质修养。<br />
<br />
　　我并不据此贬低柴玲，但不由得要想：在风云突起之际，在广场政治之中，人们要怎样才能更清醒和理智？尽管我经常说潮流中的弄潮儿往往并不是素质最好的人，但我们还是希望能够将更恰当的人送上潮头。<br />
<br />
<br />
第四，柴玲1989年6月8号录音讲话<br />
<br />
　　有人说，《天安门》为什么不引用柴玲1989年6月8号录音讲话。我也把这个讲话稿全文和封从德版全文一起另帖发布，有兴趣的朋友可以认真阅读。<br />
<br />
　　该讲话仍然是柴玲一贯的风格，不负责任。比如：<br />
<br />
　　“10点钟，……而周围东西长安街上已经血流成河。”“……我们事后才知道，……还在帐篷里酣睡的时候，坦克已经把他们碾成了肉饼。”“……事后我们才知道，这帮法西斯，他们在前面，居然用机关枪、坦克车杀人，杀人的时候，后面就有人，也就是他们的士兵，把尸体送往公共汽车上，送到三轮车上，有的人还没有死，没有断气，可是已经被活活地闷死了。”“……他们对着长安街两旁的居民区，发射火箭炮”……<br />
<br />
　　用侯德健的话来评论：<br />
<br />
　　“我们是不是需要用谎言去打击那些说谎的敌人，难道事实还不够有力吗？那么如果我们真正使用了谎言去打击说谎的敌人，那只不过是满足了我们一时的泄恨，发泄的需要而已。而这个事情是个很危险的事情，因为也许你的谎言会先被揭穿，那么之后的话，你再也没有力量去打击你的敌人了。”<br />
<br />
　　顺便说，柴玲在这个讲话中对四君子当晚的作为是这样叙述的：<br />
<br />
　　“直到最后，四位绝食的同胞，侯德健，刘晓波，周舵，他们实在忍不下去了，他们说孩子们你们不要再牺牲。但是，每一个同学都非常坚定。他们去找军方谈判，找一个所谓负责，清理，执行戒严任务的，指挥部的一个军方人士谈判，说：我们撤离广场，但是希望你们保证同学们的安全，我们和平地撤离。这个时候指挥部在征求广大同学的意见以后，是撤，还是留。决定把所有同学撤离。”<br />
<br />
　　而侯德健在《天安门》中说：<br />
<br />
　　“当时柴玲就跟我们讲，说赵紫阳和阎明复听说要希望学生能留下来坚持到天亮。刘晓波就告诉她说，我不管这个事情是真的是假的，没有任何人有权利拿现在广场上几千上万的学生的生命来作赌注！”<br />
<br />
<br />
第五，结语<br />
<br />
　　这场运动是中国民主运动的起点，但这场运动给中国带来的，也许是某种意义上的倒退。历史无法假设，也无法从头再来，争论如果学生做出怎样的让步就一定会怎样或就一定不会怎样，毫无意义。在丧失权力的威胁下，屠杀是必然的，这是中国的“社会主义”特色。<br />
<br />
　　我说的倒退，是指运动之后：国人从“恨腐败”变成“恨自己不能腐败”；知识精英大批转头下海，甚至卖身投靠；权势集团借势完成了从意识形态党向权势利益党的转变，国有资产流失殆尽；对言论的遏制变本加厉；整个国家，除了伴随着资源流失和环境恶化的GDP增长，最后只剩下当众手淫式的自誉。当然，我并不是说这一切是这场运动带来的，更不是说要由柴玲或某几个学生领袖来承担责任。<br />
<br />
　　我说的倒退，还在于民主进程此后的种种不如人意。身处民主社会的往日精英们，派系林立，内斗不断，网络上已经有太多的批评。我体谅流亡者的难言苦衷，不再这里多说了。国内呢，以06年的“王余拒郭”为例，虽说不上派系，但起码看不到应有的团结和协同。不负责任的无端指责，没有证据的诛心，罔顾事实的妄言，这种为了“正确目的”的“不择手段”，如果一直伴随着争取民主的过程，――现在网络上常见的指责是：这帮人如果掌了权，会怎样？我会回答说：如果建立了民主秩序，受到监督的“恶魔”，也远比自称代表人民的伟光正好。但，对已经发生的不足，做深刻的反思和检讨，以迎接未来，不是更好吗？<br />
<br />
　　对历史的假设，只是反思自身的一种方法。假如学生们少一点失误，那场运动会有什么样的结局？假如那场运动获得更大意义上的成功，中国会怎样发展？从意识形态党向权势利益党的转变能止步吗？中国的民主进程，后面的步伐会是怎样的？这一系列问题，也许都得不到结论。问题是：你做了一件事情，你能说这事做得完美无缺，不需要任何反思吗？既然是中国民主进程的起点，对一定会面临的下步进程，不需要“回顾历史，检讨过失，提高自身，迎接未来”吗？<br />
<br />
<br />
北国游子<br />
二零零九年十月二十五日</div>

							
							
							
							
