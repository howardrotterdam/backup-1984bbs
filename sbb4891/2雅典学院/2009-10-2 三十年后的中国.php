同时，很多公司为了办事方便，再次购买汽车，车辆累计增加百分之二十。<br />
2010年1月，公共交通不堪重负，大家发现原来奥运期间很多为了迎接奥运而被赶回老家的外来务工者都回来后，公共交通拥挤不堪。政府决定增加公共汽车数千台。一台公共汽车的道路占用面积和时间等于私车的数倍，等于车辆增加百分之五。<br />
2010年2月，外来务工人员回家过春节，北京的公共交通压力缓解，市民提议，反正北京也发展得挺好，差不多齐了，于是对外来人员索性也实施限行，凡是外来务工人员身份证尾号是单数的只能135选择使用公共交通工具出行，尾号是双数的，246可以使用公共交通工具出行，周日一概不准出行，市民有随时根据口音要求外来务工人员掏出身份证检查的权利。但因为操作困难，而且激化矛盾，政府没有批准。<br />
2010年3月，大批人员回京。北京市开始出现打车困难的情况，的哥的收入超越白领，重新成为新宠，嫁北京的哥成为部分女性的新口号。政府增加了大量的出租车，因为出租车可以每天行驶，等于车辆增加了百分之十。<br />
2011年，因为卡车不受到单双号的限制，所以很多市民开始选购卡车。改装车开始流行喷成出租车色，以混淆视听，后来被交管部门严格查处。<br />
2011年10月，因为车辆的不正常增长迅猛，老百姓们发现交通又开始严重拥堵。后来大家发现，在原来的政策中，为了一视同仁，公车和私车一样，也是要根据限行规定行驶，所以，导致公仆出行不便，本来办事就不利，现在更不利索，所以在两年内，各个部门灵活――注意，是灵活――使用政策，导致公车数目是原来的一倍。车辆累计增加百分之三十。<br />
2012年，大家发现北京实施单双号以后，车辆总数已经超过实施前的一倍，交通完全陷入拥堵。大家渐渐想明白，原来限行对于公家公司和有钱人是没有影响的，最后惨的还是工薪阶层。<br />
2012年，因为停车位不够，四环和二环原来的奥运专用道挪到最右边，改为午夜停车道，在10点后和早上7点前可以免费给市民停车，但7点以后还停着的，罚款一千元。因为经常有人睡过头而遭到巨额罚款，政府在每天早上的6点半拉响防空警报，提醒市民该挪车了。<br />
2012年5月，北京各大广场和公园开辟夜间停车场。<br />
2012年6月，天安门广场被迫夜间开放成为停车场。<br />
2012年10月，国庆期间爆发小规模游行，要求政府改变政策。<br />
2012年10月，游行停止后，政府开始重新审视限行政策，政府官员觉得，现在骑虎难下，因为现在的汽车总量是原来的一倍，如果不限行，那么等于交通负荷是2008年奥运会前的百分之两百，肯定瘫痪。<br />
2012年11月，耗时一月，政府突然想明白，原来一个人是不能同时开两台车的，就算取消限制令，也不会所有的车都上街。<br />
2012年12月，解限令开始试执行，在试执行的三天里，所有车辆都可以上路。政府开始鼓动百姓将自己的第二辆车二手卖出。<br />
2012年12月1日，试执行日第一天，会开上车的都开车了，本来一个家庭夫妻开一台车，现在夫妻可以各开一台，车流量果然增加了一倍左右，而且事故频出，交通立即瘫痪。<br />
交通一直瘫痪到2012年的12月4日，美国的卫星监控到北京的交通三天内没有任何变化，车都是停在路上丝毫未动，以为自己的卫星坏了，运送太空人去维修，耗资数千万美金。我国终于赚到美国的便宜。<br />
2012年12月10日，交通终于恢复正常，重新开始实施限行令，交警刑警武警特警和城管全部出动维持秩序，政府拟制定更加严格的限行令，并计划限制私人购车。<br />
2012年，因为交通拥堵和常年不限制公共汽车和卡车的尾气污染，北京的空气质量变得更差。同时，大家发现从阜成门到长虹桥需要耗时四个小时。<br />
2013年的4月1日，时值单双号禁行的整整第四年，北京爆发了有车一族的大抗议，所有的车辆都停放在路上，导致交通再次完全彻底瘫痪。政府出面调节，军队进入北京，但因为从六环就开始交通瘫痪，所有的军车都被堵在六环以外，军队只能步行进入北京城内。<br />
2013年4月2日，局势再度紧张，北京所有的公司单位都宣布放假，北京终于实现了所有人一天不开车的梦想。<br />
2013年4月3日，济南军区的二十万陆军进驻北京，以防万一。但这次奇特的抗议中，老百姓都选择了呆在家中，因为反正车都停满了街道，政府也挪不走。大家都在家中吃喝玩乐，扑克牌涨价为20元一副。这次抗议被政府命名为和谐抗议。<br />
2013年4月3日晚，一架巡逻的直升飞机发生机械故障，坠落在车堆里，为了不激化情绪，政府赔偿了车主原车价。<br />
2013年4月4日，经过了三十六次会议，解决方案并没有出台，但政府承诺在两年内给予解决，同时督促各大公司命令员工必须上班，否则扣工资。市民纷纷开始挪车。但仍有几千台车停在长安街等主要交通要道上，据查明，并不是因为继续抗议，而是因为狂欢太甚，车钥匙遗失。因为拖吊车辆容易引起损坏，所以军队的士兵负责将车抬到路边，待车主将钥匙配好后再取。此举引起市民的同情。<br />
2013年4月10日，政府削减百分之三十的公车，令一汽奥迪等企业回收并二手卖到中国其他城市。危机暂时缓解。从阜成门到长虹桥耗时减少为2个半小时。<br />
2013年6月1日，因为私车长期停放在外，北京在这几年内成为了国际盗车走私集团看中的城市。北京的私车偷窃率达到了百分之十。而政府乐于看到私车减少，所以采取了睁一只眼闭一只眼的态度。<br />
2013年，朝鲜在秘密核试验中发生蹊跷失误，而且当初为了躲人耳目，朝鲜选择的核武器研发基地并不是在荒郊，而在平壤的市中心，所以市民和领导人的损失外界难以得知。由美国韩国日本等国家组成的联合部队迅速进入朝鲜。朝鲜改宪，同时开始改革和开放。平壤改址，朝鲜迁都。<br />
2014年，为了巩固新政权，让朝鲜人民感受到资本的好处，临时朝鲜政府决定分批送每个市民家庭一台汽车，但因为购置新车成本太高，所以选择了走私车，而所有的走私车都偷自北京。<br />
2014年，朝鲜开通互联网，有车开的朝鲜人民喜悦的将自己的爱车贴于网上，北京市民发现那些都是自己的车。反韩情绪逐渐变成了反朝情绪。车辆丢失的北京市民表示抗议，联合国和国际刑警开始介入和解决这个奇怪的问题。<br />
2014年中，朝鲜领导人其实没有在核爆炸中丧生。朝鲜民主主义共和国复辟。之所以选择一年后复辟是因为国际社会不能在一年内改变朝鲜人民的想法，而投入了大量的资金来改造朝鲜和消除核污染。复辟后的第一件事情就是当局宣布焚烧所有来自北京的汽车。<br />
2014年中，幻想着能把汽车拿回来的北京市民彻底绝望。几万台汽车燃烧的黑烟飘到北京，在空中幻化成三个字&shy;――单双日。一天后，“单双”两字随风飘逝，只剩下一个“日”字在空中长久不散，成为奇观。中朝民间关系紧张。<br />
2015年1月，政府终于颁发了新规定，每个家庭限买一辆汽车，每个公司根据公司规模限制购车量，每个个人名下只得有一辆汽车。限在三个月内将多余的汽车全部二手处理，否则没收。<br />
2015年1月中旬，规定颁发后，有将近五十万人开车离开北京，选择其他城市居住。天津等地房价暴涨，北京房价暴跌，人们纷纷离开北京，房地产企业纷纷破产，而因为交通不畅，很多市民选择在家上网不出行，网络更红，SOHO被SOHU收购，东方新天地改名为东方网新浪天地，万科改行造汽车。<br />
2015年1月下旬，为了防止社会精英的流失，政府令所有的车辆必须在北京交易，北京牌照的车不得私自驶离北京。<br />
2015年2月，因为有大批人抛售二手车，所以二手车价直线跳水。24万元买了的车，行驶2000公里只能被8万收购，而且因为普通二手车商没有那么多的现金储备，所以最后只剩下了三家新冒出的二手车商可以现金低价接车。<br />
2015年3月28日，媒体爆料，那三家现金充裕的二手车商原来是政府背景，因为8万收的车到了外地以后可以卖到16万，其中有两家二手车行是太子党所开。<br />
2015年4月1日，再次爆发大规模的示威游行，要求政府原价将车辆购买回去，并且有人员上街。北京市的几百万辆车再次停在各条马路上。出现暴力事件。因为各个进京道路的道口都有群众的几百台车停着堵路，部队被拦截在北京直辖市外行进缓慢。<br />
2015年4月1日晚，台湾出动四百余架战机飞往北京，配合凌晨发射的几百枚导弹，企图对混乱中的北京实施突袭。规模之大企图一击致命。美日和欧洲国家从卫星上发现了异常，但是都选择了静观其变。我们的朋友非洲国家们是次日看报纸才获知了此消息。在敌方的导弹发射后，早上六点多，北京城拉响防空警报。但是由于正值抗议期间，所以没有人起床挪车.<br />
同时，解放军在第一时间发现异常，我军的导弹拦截系统和数百架战机起飞。<br />
几百枚导弹中，被我军拦截了百分之八十，其中有数十枚是解放军驾驶战机直接拦截。但不幸二十多枚导弹落在了北京，因为没有人理会防空警报，所以平民的损失惨重。在空战中，敌人的几十台飞机突出重围，向北京飞来，其中包括几台重型轰炸机。在这千钧一发之际，敌人的飞机突然全部坠毁。事后根据调查，敌人使用的是从中国进口的航空用油，因为油质较差，不符合标号，在第一次执行这种长途飞行的时候终于引起了引擎故障。敌人的偷袭计划失败。<br />
但由于汽车挡住了所有的道路，救援工作进展缓慢。美国等国家表示，对于导弹落在平民区表示谴责，并申明导弹不是美国生产的。非洲友国表示，他们的援军会坐船过来，预计两个月后到达中国。我国婉拒。<br />
2015年4月3日，市民们纷纷开车离开北京。<br />
2015年4月3日，中国对台宣战，在战争的尾期，朝鲜和印度突然进攻中国，西藏和新疆局势紧张，同时，朝鲜的核武器秘密研发又告失败，再次在本土引起失误核爆，韩国美国卷入战争，再次出兵朝鲜。印度和日本海军开始进攻，我国被迫使用了秘密研制了数千年的超级武器，两艘超巨航空母舰启动，在这两艘巨舰一直伪装成崇明岛和海南岛，其中崇明岛号开往日本海峡，海南岛号负责对付印度。这两艘超级武器惊动了世界。民众终于明白，原来这么多年，我国假装腐败，转移国际视线，其实巨资都用于研发这两艘超级武器。而大家惊奇的发现，以前因为贪污问题被抓起来的重要官员，其实都是为了掩护研发的进程而做出的牺牲，如今，他们已在海南岛号和崇明岛号中承担重要的军事指挥任务。全国媒体为他们平反。<br />
2016年，综合各个因素考虑，中国迁都，北京不再是首都。<br />
2016年，俄罗斯等欧洲国家卷入战争，第三次世界大战爆发。<br />
因为中国的超级武器的出人意料和坚不可催，中国获得了战争的绝对优势。战争进行到了最后成为了资本主义国家和社会主义国家的决战，欧洲国家宣布退出战争，崇明岛号镇守我国，海南岛号则开往古巴进行援助交际。<br />
2018年，中国发现整个澳大利亚正以20海里的速度向中国开来，动机不明。原来资本主义国家和我们想到一起去了。这是资本主义国家最后的疯狂。中国政府考虑是否要动用终极超绝密武器，那就是中国在古代发射的一颗卫星，而这颗卫星的撞击足以毁灭一个大洲。秦始皇焚书其实不是为了坑儒，而是为了销毁民间关于这颗卫星的史料记载。唐宋明清等朝代为了掩饰这颗卫星，所有的诗人都要纷纷写诗赞美。而这颗卫星的巨大控制开关位于央视大楼的地下，央视之所以造成裤衩造型，其实是因为大楼整体连接这控制开关，当裤衩掰开的时候，地下开关就能启动。<br />
美国曾在六十年代发射火箭登陆过我国这颗隐藏的卫星――也叫月球，引起了全世界的轰动。<br />
2018年中旬，澳大利亚行驶过程中所造成的海啸又淹没印尼，同时，为了节省燃料和不贻误战机，澳大利亚决定不绕远路，直接从印尼上驶过，印尼从地球上消失。<br />
决战就要到来……<br />
你看，一切都是由单双号限行引起的。<br />
