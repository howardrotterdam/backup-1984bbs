													<h2>摩罗“倒戈”与革命党的暴政归宿</h2>
						
						
																											<div id="postmessage_498505" class="t_msgfont">摩罗倒戈了<br />
<br />
摩罗“倒戈”与革命党的暴政归宿<br />
<br />
<br />
<br />
&nbsp; &nbsp; 摩罗最近出了一本新书《中国站起来》。那天在天河书城的“显著位置”，（在学而优却没有看到这本书，开来书店的品格自有高低），我把这本书拿在手里掂来掂去，里里外外地翻看，从目录到内容，从豪华的推荐阵容，到摩罗油头粉面的照片。我肯定不会花钱买它，但我不忍放下，我不知道自己想要找什么。我过去买过，读过摩罗的书，就是那本让他博得大名的《耻辱者手记》。那本书里的摩罗给我的感觉是是一个外表瘦弱，但内心坚毅的人，那本书里没有他的照片，但是，觉得他应该是那么一个人。那种感觉来自两个方面的内容：他书中写俄国革命党人和关于中国国民性的部分，给我造成他坚毅形象的一面，而他书中写他自己苦难经历的内容（在海南流浪的部分），给我造成他瘦弱形象的一面。说实话，我对摩罗的文字不怎么喜欢，我觉得他写自己经历的那些文字特别幼稚和做作，像中学生作文，但是，他写文化感悟和精神批判的段落，尽管文字的质地一般，但是里面有一种高贵的气质，这是他让我感动、尊敬的地方。当时，我的感觉是：这个人是为整个民族背负十字架的人，他就是那个人――耶稣！摩罗的那本书我反复咀嚼，内心的激动久久不能平复。<br />
<br />
　　大概10年前，有一个朋友，他有一个同学是华东师大王晓明的学生，据说认识摩罗。他说，有关部门给摩罗一套房子，条件是少说话，但摩罗拒绝了。这件事让我对摩罗更加敬佩不已。因为，只有高贵的心灵才能做到这样，像我是做不到的。后来，我自己“率先”堕落，到经济学那里“依附权贵”去了，摩罗的消息也就渐渐隔膜。去年在网上读到摩罗关于五四启蒙的倒戈文章，我稍稍觉得不安――我有点不太相信。但我不知道摩罗变化的缘由，也就无法评判。后来看到萧瀚先生的文章，说：摩罗渐渐离底层远了，失去了对底层生活的痛感（大意）。话说得很客气，但是，其中的惋惜和轻蔑非常明显。但是，我仍然不愿过早地定论――我不愿打碎偶像。我想这个变化也可能是摩罗学术和思想发展的“歧途”吧，是摩罗经过长期阅读、思考和学术积淀，对自己过去思想轨迹的真诚“反动”，有势必如此，不得不如此的“命运”――我在为他辩护。如果是这样一个真诚的结果，我们作为鼓掌的“道边儿”，还是宽厚一点好，他毕竟给我们奉献过思想启蒙的盛筵！<br />
<br />
　　但是，看到摆在那里，和《货币战争》、《中国人可以说不》比肩而立的《中国站起来》，我不得不承认，摩罗完全成了“他们”。《中国站起来》几乎是一个完美的策划系列，而且，还有宋鸿兵等人的推介之辞。这时，另一个问题浮上脑际：摩罗之变可能是一个具有普遍性的“特例”，它也许代表了一种现象，也就是中国儒家文化的毒素养育出来的中国文人的必然性。把1949-1979年间的那批文人全部不要算数，Abc<strong>中国近现代史上，这样的人不少，而且他们比摩罗的影响力更大。比如，严复，从进化论的翻译者，到袁世凯的幕僚。比如，章太炎，从革命党人，到“参与投壶，接受馈赠”。当代的人里面，有一个人大家可能忽略了，就是《河殇》的执笔人之一的王鲁湘，此人曾经是海洋文明的鼓吹者，现在却坐在凤凰台上靠佛道和儒学“养颜”呢。 Abc</strong>这些人，为什么都会有这样的人生“大逆转”呢？我近来一直思考这个问题，最后，我发现这是一个聪明的机会主义计算，里面掩藏着“创业”和“守成”的关系，而且颇具中国特色。<br />
<br />
　　在一个民主和开放的社会，知识阶层中会不会发生这种“丑闻”呢？比如，一个知识精英，他以痛骂政府为务（以此创业），由于他的“歪理邪说”博得广泛的民意，从而积累了巨大的资产（创业很成功）。这个时候，政府会怎么对付他？而他本人又如何解决“守成”的问题呢？首先，政府会不会利诱他就范呢？我想没有必要，因为民主的政府其合法性来自选票，而不是权威，政府不需要这个“左派”所拥有的民意来增加其合法性。那么，政府能不能出个阴招，整饬一下这个“乌鸦嘴”呢？假如这个乌鸦嘴躲在大学里（西方的左派以躲在大学的为多），政府有没有办法让他“下课”？――民主社会，大学比政府牛得多，政府绝对不会不知趣地去碰这个钉子。如果这个乌鸦嘴躲在媒体？政府能否逼他辞职？――媒体正愁没有政府的丑闻炒作，政府那敢主动授人以柄！那么，作为这个成功了的知识分子又如何守成呢？我想他最好的办法，也是收益最高的办法就是掏空心思，更辛辣、更放诞地“羞辱”政府，他“羞辱”得越有水平，他的价码就越高。他深知――他的买家在民间，他永远不会弱智到去政府那里出卖自己，以换取富贵人生。<br />
<br />
　　在一个高度集权的社会里，情况恰恰相反。集权社会是一个高度垄断的社会，一切资源，包括人们的致富的机会，甚至个人的体面，都被政府，或者政权紧紧攥在手里。在这样的社会，知识阶层常规的晋身通道是进入体制内，即，出卖知识和灵魂求得被施舍的富贵和体面。这一途是大多数知识阶层拥塞其上的康庄大道。但还是一条另类创业的险招（也有些知识分子是命运注定），这就是从对立面做起，即，“激进”和“批评”之路。比民主社会更甚的是，在集权社会，激进和批评更容易得到民意的支持（因为更需要胆量）。但是，和民主社会不同的是民意的支持在这里是集权政府合法性的宝贵来源――这一点非常关键。一个冒了巨大风险的知识者，通过激进和批评迅速积累起巨大的民意――他成功了。下一步该怎么办！他面临一个选择：如果他是一个真诚的思想者和文化英雄（命运的安排），他就义无反顾地选择坚持下去，结局是或者饿死，或者坐牢，或者被流放，但名垂千古。如果他是一个机会主义者，他的唯一的“守成”策略就是立即选择一个时机，出卖自己的民意资本，为统治者亟需的合法性做一次一次的鼓吹――直到耗尽他的全部民意资本。我推想：摩罗就是这样经过经济理性计算之后背叛自己的过去的。<br />
<br />
　　这种“创业”和“守成”的模式很具有中国特色，因为这不仅仅是摩罗等知识阶层的终南捷径，中国社会，不论历史和现实中，种种集团和个人都是以此为“成就”之途的。比如，民意初始在山林聚义“创业”，最后却被朝廷招安，修成正果。“聚义”是为了让草寇卖命，“招安”才是山大王守成的“正果”。水泊梁山的故事是经典案例。军阀早年嗜杀成性，镇压学运，涂炭生灵，晚年却读经吃素，立地成佛。“嗜杀”是积累财货，立地成佛才能赢得身前身后的清名，这是武夫“守成”的秘诀。段祺瑞等是其中翘楚。革命党以“民主”为招徕，推翻旧制，进了京城，则实行独裁。“主义”是蛊惑民众为“自己”创业的宗教，而“独裁”才能守住“江山”姓某。这种把戏比蒋介石玩得好的大有人在。这是中国政治文化中特有的“婉约”与“阴雌”。中国人的人性没有稳定性，或者说，逻辑上不周延――没有可传递性和一致性。一个人的信仰此一时，彼一时。你如果拿一个政客在野时的所信去苛求他登上权力之巅时的作为，你肯定犯了不小的幼稚病。原来是慷慨激昂的民间派，一旦进入体制，就忠心耿耿，心安理得地做走狗。你拿性命支持一个“好人”，一旦成王，却成了恶魔――这种循环进行了数千年，至今没有终结。摩罗的倒戈比起上面这些风云际会的人物来，真是草芥大的一个小玩意儿。<br />
<br />
　　这几天心里闷闷的，就翻出摩罗的那本书《耻辱者手记》，随机地翻看里面的内容。我觉得那个把眉毛拧成绳子一样批评国民没有耻辱感的人，那个抚着自己的同情心镇日悲悲戚戚的人，他过去身上凝聚的那一份高贵和真诚在他以《中国站起来》宣布做一个自信高贵的中国人之后就荡然无存了。我直觉得他笔下洋溢着的那些沉重的使命感，那些独立的思想，那些高扬的精神，原来都是伪装出来骗人财货的圈套。我的眼睛停留在摩罗下面两段文字上：<br />
<br />
　　“所有奴格的典型代表，无疑就是中国文人。让自己去做这样的典型，岂不是感到恐惧。如果我一生的努力只不过是把自己的名字写到郭沫若杨朔们的尾巴后面，这是我无法接受的奇耻大辱。<br />
<br />
　　“现在我终于知道，做一个文化人乃是我不可逃脱的命运，也是我别无选择的选择结果。选择的同时我抱定了一个原则，那就是必须坚持自我。我可以没有恩宠没有地位，可以没有名气没有桂冠，但我绝不可以没有自我，绝不可以没有独立的人格。我一定要把自己与中国文人区别开来，与一切中国奴格区别开来。倘若他们自视为救主，我就甘为叛神，倘若他们自视为神灵，我就甘为邪念，倘若他们自视为人，我就只有做魔鬼。1986年我自己取名摩罗时，便是这番寓意。同时我知道，我的灵魂早就被奴隶的气息所浸透，要完全摈弃奴性哪是一个中国人所能轻易做到的？我只是怀着战战兢兢的虔敬之心，按着自己的要求尽力去做。”（《耻辱者手记》213页）。<br />
<br />
　　“盖棺论定”――不知道西典里有没有同样意思的说法。如果仅为中国汉语文化所独有，那么，它确是我们的先人设计出来，为“防范”站起来的“大国民”的人性奸诈的――不把这些人的尸体装进棺材，你就无法定论他们究竟是什么样的货色！万幸得很，摩罗不在此列！摩罗早早地用自己的文字把自己装进自己打造的“奴格”里面，并将自己的名字牢牢订在杨朔之前郭沫若之后的位置上！<br />
　　<br />
<br />
