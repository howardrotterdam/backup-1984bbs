													<h2>摩罗：老百姓就该跟一切强权争权夺利</h2>
						
						
																											<div id="postmessage_498373" class="t_msgfont">老百姓就该跟一切强权争权夺利<br />
<br />
《精品阅读》主编老愚：这也是某些批评者把你称为民族主义的原因吧。我理解你这种变化，而且我认为你这本书是写给五十年后的中国人看的。那是祛除了奴性的中国人，是彻底洗去被蹂躏心灵挫伤，有文化自觉、民族意识高涨的中国人，不是当下的东西。是超越了现有利益格局的东西，不是一个时评。我理解你呼唤的是中国人特性的回归，包括精神和文化――<br />
<br />
摩罗：对，这种描述非常准确。我是想给国人某种警醒，三十年来，我们一直是自觉融入国际上那种制度框架和权力框架，甚至不惜一切代价，以为融入那个框架之中，就什么问题都解决了。其实不是这样，这样的姿态对国家利益是不利的。我希望国人能从中醒悟过来：不要带着崇敬之心屈从别人制定的游戏规则，也不要带着虔敬之心认领别人制造的国民劣根性枷锁――事实上，好多中国人已经认可了自己有劣根性。我们重要的是学会平等地跟西方强权相处，学会跟他们博弈，以此来维护国家利益，从而也能维护每个国民的个人利益。<br />
<br />
老愚：鲁迅他们所描述的劣根性，如果说原来没有，但现在恰恰有了，有着很明显的特性，你要恢复的是以前那种东西，假定存在自然状态中国人的话。现在说我们不比其他民族更不道德还是有困难的，这个能否请你说几句？<br />
<br />
摩罗：我想，近百年所形成的未必是你所认为的不文明的东西。生活中的行为规范等，我认为都是很表层的东西，是可以改变的，未必能上升到文明的层面来谈。这是一个行为习惯问题，可以调整啊。比如随地吐痰、乱扔垃圾等等，这并不是什么了不起的劣根性。中国人随地吐痰，欧洲人随地杀人――从以前的印第安人、非洲人、中国人，到今天的伊拉克人、阿富汗人，他们走到哪里杀到哪里，有一个欧洲人在上海外滩甚至一脚将一个中国人踢到黄浦江里淹死了，理由是那个中国人阻碍了他走路。随地吐痰与随地杀人，哪个更加劣根？为什么我们不批评随地杀人，却天天拿随地吐痰上纲上线？<br />
<br />
相信自己有劣根性并且不愿意摆脱出来，恐怕才是我们国民性中最不好的东西。这是两百年失败的经历塑造成的心理扭曲，又被五四精英误导，对我们失败的政治经济军事原因都轻轻带过，不予深究，却全部归结为文化问题，归结到国民劣根性问题，这在认识上是完全错误的，在政治上等于是为殖民者帮忙，殖民者要的就是你这种体认――虽然五四精英的初衷不是这样，但实际起的作用就是这样。<br />
<br />
当下中国有很多人出现了国家认同危机：中国什么都不好，外国什么都好；中国游客做什么都不文明，外国游客做什么都有创造性；中国领导人在外交场合做什么都错，西方领导人怎么欺负弱国都是对的。我们从五四以来已经建构了这样一个理解世界的模式。我这本书就是要打破这种错误的世界图式。伊斯兰世界的精英群体就从来不这样引导自己的民众。在西方世界殖民冲击下，他们的失败比我们更惨，但是他们就从来不认为自己是劣等的、西方人是优等的。<br />
<br />
老愚：好多人可能认同你的见解，希望内心里认同国家，但一回到现实，立马就感受到完全相反的东西，你怎么让他们从这种困境中摆脱出来？<br />
<br />
摩罗：现实的很多问题是明摆着的。我也生活在这个环境里，我非常理解他们的情感。现在是要解决问题，不能像一个文学青年那样执着于某一点。中国作为一个弱势大国，其发展是一个非常复杂的系统工程，每个人都被裹挟其中，这里面有许许多多问题。不能因为我这本书没有谈某一个问题，就骂我背叛了什么什么东西。<br />
<br />
老愚：我似乎能接受你对劣根性问题的分析，就我个人而言，鲁迅所塑造的祥林嫂、孔乙己、阿Q等形象，甚至影响了我对一个社会的判断，这些病态、边缘化的人物，笼罩了我对中国人的认知。这种人性描述的格式化，被你这本书彻底去势了，我觉得你这本书的价值主要在于此。还有另一种中国人和中国人的生活，比如你所推崇的 费孝通先生所描述的那种图式。但孔庆东等人还是不认可你对鲁迅的批评，你怎么看？<br />
<br />
摩罗：你点中了我心底最痛的东西。我这两年的写作，最用力的就是如何不带偏见和歧视地对待底层人群。《中国站起来》有个姊妹篇，即将由复旦大学出版社出版，就探讨了你说的问题。鲁迅面对殖民者时当然代表了中国的利益（这比现在的精英好一些），可是面对底层的时候他只能代表精英的立场。为什么五四精英要这样描述国民劣根性？他们实际上将殖民者批评中国全体国人的整体性的劣根性，迅速转嫁到底层群体身上，把自己撇个干净。<br />
<br />
他们通过批判底层人的劣根性而成为文化英雄、国家的主人，跟殖民者一样拥有了道德优势和社会优势。他们把中国失败的一切原因都归结于底层人，他们自己跟西方人一样伟大。你看鲁迅小说所表达的意思，中国为什么打不垮皇权？因为七斤不愿意剪辫子（《风波》）。中国文化为什么如此愚昧？因为闰土抱着香炉求神拜佛（《故乡》）。封建礼教为什么这么难于扫除？就因为底层人麻木而又迷信，他们只想着阴曹地府，想着捐门槛（《祥林嫂》）。精英人物的理想为什么总是不能实现？因为华小栓要吃精英人物的鲜血（《药》）。革命为什么无法胜利？因为阿Q企图借革命抢到一张宁式床供自己所用（《阿Q正传》）。鲁迅的小说为什么如此受到精英群体的追捧？因为一读鲁迅就能知道自己是精英。<br />
<br />
自从毛泽东立国以来，为了挺直腰板做人，统治集团让底层人付出了什么样的代价？以前是为了国家而饥饿，现在是每年牺牲四万根手指。那些批评我背叛底层的人，不知道我跟底层是什么关系。<br />
<br />
《中国站起来》的主题之一，就是追索中国劳动者创造的财富，究竟去了哪里？贪官污吏拿走了一部分，可是大部分都被外国人拿走了。朱F基说某件产品中国人只能挣一美元，美国资本家却能挣二十多美元。中国的贪官污吏只能在这一美元中分赃，另外二十多美元却能将美国人养成八旗子弟。<br />
<br />
我强调中国要挺起腰板维护自己的国家利益，意思就是别让老百姓的钱都流进贪官污吏和洋八旗子弟的腰包。国家应该成为老百姓财富的保护者，老百姓自己尤其要摆脱国民劣根性的自卑体验，挺起腰板跟洋强权、土强权争权夺利――这才是《中国站起来》的本意。<br />
<br />
