													<h2>当前中国最坏的六种人</h2>
						
						
																											<div id="postmessage_531305" class="t_msgfont">当前中国最坏的六种人<br />
文章提交者：_之 加帖在 猫眼看人 【凯迪网络】 <a href="http://www.kdnet.net" target="_blank">http://www.kdnet.net</a><br />
<br />
当前中国最坏的六种人<br />
<br />
1，民主的敌人，比如形形色色的专权者。因为，专制或者说缺少民主，是中国一切政治经济社会弊端的总根源。<br />
<br />
2，愚昧的国民，比如形形色色的左粪（或者说得直接一点，范围小一点，就是文革余孽。）因为，愚昧的左粪是滋生中国专制政治的一大温床和思想文化基础。<br />
<br />
3，贪污腐败的官员，包括企事业单位各类干部和专业技术人员（他们手中握有的也是一种权力资源）。因为，贪污腐败是是中国一切政治经济社会弊端及其他一切不公平现象的集中体现和直接原因，也是中国专制政治的必然结果。<br />
<br />
4，猖狂致富的不法奸商。但是他们总体上还得仰仗贪官污吏。<br />
<br />
5，学术虚伪、学术愚民、没有良知、为虎作伥的知识分子。他们在思想本质上或经济政治利益上与贪官、奸商沆瀣一气，分一杯羹。<br />
<br />
6，黑社会、偷扒抢骗等刑事犯罪分子。但是他们中相当一部分只是社会腐败和社会不公的派生物。<br />
<br />
最后，要说一句的是，中国的老百姓，我们的父老乡亲，还是好人多，尽管他们也是凡夫俗子，在这个世俗的社会，免不了有这样或那样的缺点。总之，世上好人多，中国也不例外，这是我，作为一个中国人的起码的信念。<br />
<br />
<br />
关于第2类人，再加几句：愚昧之所以坏，在于它也是暴力的最大温床。另外，我所谓的左粪，只是借用一种说法而已，指的是为毛在文革中的行为涂脂抹粉，而且鼓吹以文革的方式来解决中国的问题。或者说，我所谓的拥毛者，特指那些封建子民意识浓厚的人，他们是愚民政策的受害者。妄想依靠文革之类的手段去消除腐败，只是饮鸩止渴、画饼充饥。从本质上看，越专制越腐败。改革前的腐败只是形式不同而已。<br />
<br />
要说明的是，中国年轻的左粪和经历过文革的左棍，与民主政体下的左派，完全不可同日而语。中国年轻的左粪，只是中国特色的愚民教育的产品而已，中国的左棍，只是专制制度的吹鼓手而已。<br />
<br />
中国从来就没有民主政体意义上的左派,只有中国特色的左棍.即文革余孽.和愚民教育下形成的无知的左粪.<br />
<br />
左粪的两大特点:对国内事物,有意无意地维护专制,为虎作伥;对国际事物,拒绝融入国际主流政治文明,盲目排外.尤其对发达国家,有红眼病。因此，对外，左粪们为流氓国家甚至恐怖主义鼓掌，与文明国家为敌。对内，左粪们甘愿做维护专制当权者的奴才，与弱势群体为敌。虽然也有一部分左粪号称关注民生，但是，从维护人民的基本权益来看，他们的思路根本上是南辕北辙。弱势群体将被榨干，这主要是由于不民主的体制。<br />
有比较好的民主体制的国家，也存在某种贫富分化，但是在很大程度得到民主体制的制约，简单地说，受选票的制约。但是，在一个不民主的国度，是没有这种制约的，所以弱势群体很难维护自己的权利。<br />
特别要指出，别把维护弱势群体利益的事寄托在一个专横的暴君身上，那样只能是更大的灾难！再说多一句：在一个专制集权的社会里，民众缺乏政治权利而想维护自己的经济权利，就如同赤手空拳的妇女，面对破门而入的强盗，想求他发善心一样的可笑！寄托于集权体制的暴君，那当然是缘木求鱼，就更可笑了！<br />
<br />
<br />
再加几句：<br />
1，文革前和文革中的灾难、改革后的弊端，所有的问题，只有一个共同的根源，那就是制度的缺陷，那就是没有民主政体。这个问题没弄清楚，就可以称之为左粪。<br />
<br />
2，中国特色的左粪，与民主政体下的左派，完全不是一回事，绝不可相提并论。而一些左粪们，则千方百计混淆、模糊二者的区别。<br />
<br />
3,民主和专制，一好一坏，看来已经是无庸置疑的了，所以，左粪们就经常提出一个看似奇怪而又正常的问题，那就是：动不动就说别人是伪民主。但我从没看见有人说左粪们是伪专制，只是直接了当地说他们是专制奴才。<br />
<br />
<br />
附带说明一下：本文决非从人性上去认识和解决问题，恰恰相反。本文隐含的主题是一个： <br />
改革前的灾难、以及现实的腐败，根源只有一个，没有两个。那就是缺少民主，缺少一种人民参与、人民决定的制度上的有法制的民主。虽然民主制度不是唯一重要的，但肯定是最重要、最根本的。</div>

							
							
							
							
