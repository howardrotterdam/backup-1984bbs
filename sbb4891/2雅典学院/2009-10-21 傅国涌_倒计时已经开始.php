九十年了，“五四”依然是一个能让许多中国人心跳的日子， 20年前，我走向广场的时候，打的旗帜就是“纪念‘五四’七十周年”。在我心目中，“五四”不仅是学生街头运动，不仅是白话文、新文学，也不仅仅是提出了民主与科学，“五四”有着更丰富、更复杂、更可贵的内涵。在“五四”九十年即将到来时，我想得最多的是，“五四”时代给这个古老民族到底留下了什么样的精神遗产？我们今天与“五四”有多大差距？<br />
<br />
长期以来，人们对于“五四”的认识，要么从政治史出发，要么从思想史出发，很少从社会层面去认识那个时代，了解那个时代不同人群的生存境况，和他们的所思所想，他们的梦想与追求，从而展开一幅幅更逼近历史原貌的画面。一个时代的幽暗和光亮，只有进入到社会的不同层面，才有可能看得更清楚。政治史总是一种宏大的叙事，思想史重视的是代表人物的思想、观念，它们都具有极强的选择性，如同一个筛子，常常把许多活的细节过滤了，变成一些已整理得井井有条的知识。而真实的社会生活常常是参差不齐的，如同一棵没有被修剪过的树，枝条交错，往不同的方向生长，然后枝上满是叶子，每一片叶子都以自己不同的姿态去承受阳光和风雨。一句话，历史的原貌要更加丰富，更加多样，呈现出更多的不规范。历史不是纸上写成的，历史只是已逝去的生活，尤其在“五四”那样一个大时代，许多不同的侧面都值得我们去关注，那原本是一个有着无数的不确定性，无数的可能性的时代。<br />
<br />
 “五四”的中国诚然是一个很不完美的社会，否则袁世凯称帝、张勋复辟这一幕幕闹剧也就不可能接踵登场，在这些闹剧背后都有着深层的社会原因，当时流行的“黑幕小说”已把社会糜烂、荒唐的一面用小说笔法展现出来。一个没有经历过启蒙运动洗礼的古老民族，即使把皇帝赶下了龙廷，也不可能在一夜之间获得自我更新，新生的共和制度只是建立在沙土之上，而不是磐石之上。但是，千年王权的终结，旧权威的消解，迎来了一个可以独立思考、自由追求的时代。“五四”最大的特征就是它的多元和开放性，众声喧哗，没有权威，否定偶像，各种思潮、价值相互冲撞，相互融合，整个社会呈现出一种前所未有的生机和活力，那个时代的人，特别是受过新式教育的年轻人，很少有唯唯诺诺的，多是有棱有角的。<br />
<br />
以往，在谈论“五四”时我们常常听到个性解放，思想解放，女性解放这些说法。这些说法都没有错，以《新青年》、《新潮》这些新锐期刊和一些报纸副刊为平台，年轻一代的思想探索迅速越过了传统的藩篱，热烈地拥抱来自异域的林林总总的新思潮、新主义，他们向往并尝试全新的精神生活。<br />
<br />
至于女性解放，一方面不断有北大向女生开放，女青年进入银行和百货业等新闻出现，一方面贞操问题成为知识分子讨论的热点话题之一，女性为反抗包办婚姻而自杀的消息也时有所闻，并成为社会关注的焦点，1919年11月湖南长沙的赵五贞自杀，青年毛泽东就在当地的《大公报》、《女界钟》等媒体连续发表了十篇评论，为恋爱自由、婚姻自由大声疾呼，甚至喊出了“赵女士万岁”的口号。在这些解放的后面，最本质意义上的解放应该是社会解放。只有社会解放了，个性解放、女性解放才不至于成为空话。如果社会不解放，思想解放就没有地方可以安置，无法在大地上落实。社会解放乃是社会的自我解放，最终要依靠每个人自己解放自己，而不是某个救世主来为你做主，来解放你。这应该是“五四”的核心命题，德先生、赛先生也好，讨论女子贞操、娜拉出走等问题也好，打倒孔家店也好，这些讨论都可以看作是社会自我解放的一部分。这是“五四”留下的最重要的精神遗产。<br />
<br />
九十年后回望“五四”，可以说，那时候已出现了公民社会的雏形，经过晚清以来几代人的努力，社会在王权的枷锁下渐渐挣脱出来，拥有了一定的自主力量。“五四”一代知识分子也是从臣民向公民转型的一代，他们的公民意识，社会责任感都被激发出来了。他们的个性最大限度地得到张扬，他们的创造力也在各个不同的层面展现出来。“五四”新青年无论以后走上了哪一条路，至少，他们都可以尽情地追求自己的理想，可以无所顾忌地发表自己的见解，可以选择自己的生活方式，这是他们的权利。<br />
<br />
在本质的意义上，我们可以说，“五四”不再是一个国家完全主导社会的时代，社会自身拥有相当多的自主性，人们生下来仿佛就拥有了思想自由、信仰自由、言论自由、新闻出版自由和结社自由，可以随意地创办自己的报刊，可以随意的结社。从基本权利的状况来判断，社会确实从国家的桎梏下解放出来，获得了前所未有的自由度。<br />
<br />
今天中国最大的问题恰恰在这里，大政府，小社会，社会仍处于国家机器的宰制之下，动弹不得，在高度的行政主导之下，权利缺位，权力蛮横，个体生命只能臣服、依附于国家的齿轮下，失去了创造力。权力的膨胀和权利虚化，国家的推土机在大地上肆意推进，社会几乎连呼吸都困难，即使在娱乐自由、追求财富的自由背后，也能处处看到那双隐约的操纵之手。国家高于一切，凌驾在一切之上，官权通吃，把手伸进了生活的大部分领域，与毛泽东时代不同的只是，国家还给了个人求生存的一点点可怜自由，30年来的中国如果说有什么进步也都是这点可怜的自由带来的。遥望“五四”，习惯了不自由状态的人们可能会惊讶，甚至不相信，以为那是一个虚构的时代。前些日子，浙江大学第16届博士生登攀节邀请我4月1日去做一次讲座，题目定下来了：《我们的1919：知识分子的责任》，海报也贴出去了，却突然被莫名其妙地取消。这件发生我身上的事可以为我们这个时代做一个小小的注脚，在“五四”九十年后谈论有关“五四”的话题，仍然不是一件容易的事。一切都要听命于行政，国家机器上一颗颗螺丝钉无时无刻不在防范着社会的。思想自由只是自己脑中想想的自由，言论自由只是饭桌上或菜市场发发牢骚的自由，其他的自由更加无从谈起。以国家的名义，掌握大大小小权力的人，仍然想控制一切，这就是今日中国全部病根之所在。个人没有权利，社会就不会有自由，这是九十年后的“五四”之殇。 <br />
<br />
今天，当我们在再次面对“五四”时，不应该只剩下纪念和凭吊。本来“五四”就是一个古老民族自我更新的起点，我们要回到那个起点上去，持续不断地寻求个人的自我解放，一步一个脚印地去争取自己的权利，一个健全的公民社会只能建立在个人自由的基础之上，那是社会解放的最终也是最可靠的磐石。每个人都可以立地开始自我拯救，我们期待的并不是天上掉个大馅饼，而是通过包括自己在内的许多努力，逐渐造成一个像“五四”那样的多元、开放的社会，最终以公民社会的力量来约束总是企图控制社会的国家权力。其实，在我们这里，所谓国家也不过是某个特殊利益集团假借的名义罢了，真正的国家是由每个个人的权利构成的，这是“五四”时代许多人曾经想清楚的问题。<br />
<br />
<br />
<br />
