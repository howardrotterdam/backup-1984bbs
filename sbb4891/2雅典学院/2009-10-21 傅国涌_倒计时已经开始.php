													<h2>傅国涌：以不变应万变还行得通吗？</h2>
						
						
																											<div id="postmessage_296360" class="t_msgfont">2008年的中国，一方面官方导演的一场大戏正在登场，以奥运会来彰显盛世，向世界宣示天朝威仪，向民众炫耀自己的强大不可挑战，一方面却是接连的大灾大难，天灾与人祸不断示警，毫无疑问，中国已进入多事之秋。改革已死，奥运之后中国将往何处去？我们这个苦难的老大民族还有未来吗？对于掌握了绝对优势资源的统治阶层来说，对这些问号的回答是不言而喻的，那就是原封不动地保持现有的体制，以不变应万变，尽最大可能地压制一切不满的声音、怀疑的声音、批评的声音和反抗的声音，把所有的喉舌牢牢地控制在手里，防民之口，如同防川，只许州官放火，不许百姓点灯，……这些成语的现代版都将不断上演，继续以毁灭生态环境和世道人心为代价，漫无节制地攫取世俗利益，这种贪婪的赤裸裸的末世心态，已越来越不加掩饰，至于民族的未来他们已不再考虑，我死之后或者我移民之后，哪管它洪水滔天。自以为可以驾驭万物人间的强势集团，不敬天、不畏神，对一切都没有一丝的敬畏和谦卑，任何的灾祸自然都不会放在他们的心上，他们以唯物主义者自许，在没有比他们一种物质力量掀翻他们的宝座之前，他们不会在意任何警告，不会忌讳任何的天怒人怨，他们将一意孤行到底，直到一切都不可挽回、无法收拾。<br />
<br />
&nbsp; &nbsp; 只有把暴力威胁和谎言欺骗完美地结合在一起，才能实现有效的统治。仅仅有暴力、不加修饰的暴力是不够的，与单纯的暴力相比，以暴力为后盾的谎言有着特殊的功能，当谎言一而再地重复，重复得人们耳熟能详时，人们就会把谎言当作事实，并无条件地信服谎言。这样的统治就会稳如磐石。在互联网出现之前，对于任何在某个角落发生的新闻事件，官方都可以任意做手脚，编织一个美丽的谎言，向不明就里的大部份民众宣布，然后就像什么事都没有发生过一样。在互联网普及之后，官方仍然想用老办法来应付一切，难度已变得越来越大，至少一部份已免于愚昧的人们不再相信官方的解释。官方可能无法想像，自己的铁捅江山依然固若金汤，他们说的话，竟会有人不相信，费多少口舌，效果都不明显。<br />
<br />
&nbsp; &nbsp; 对于每天要为生计奔波、为衣食而忧的大多数中国人而言，表面上看起来，他们没有余暇、没有兴趣、更没有力量来回答这些问号，除了苟全性命于盛世之间，乞求能够善终，除了无可奈何地等待之外，几乎没有更多的奢望。罗斯福当年提出的四大自由，人类应该有免于恐惧的自由、免于匮乏的自由、表达的自由和信仰的自由，在我们这块东方的土地上至今还是一个遥远的梦，说到底，我们连免于愚昧的自由也没有得到，从教科书到不能越雷池半步的教育体制，从无远弗届的CCTV到所有大大小小的媒体，哪怕是天网恢恢、疏而有漏的互联网，官方的高度垄断和严厉管制，使国人要免于愚昧变得很困难。健康的价值观有赖于真实的信息，如果从教科书上、从课堂上、从许多常见的出版物和媒体得到的历史都是经过意识形态严格过滤的，如果教育和新闻都处在某种单一导向之下，要跨越这些强有力的障碍，完成自我启蒙，生活在真实中，又谈何容易。这是官方价值仍能欺骗许多人的根本原因。因为其他的真实信息很少有机会抵达大多数人。除非你刻意去寻找，对于大多数人来说，这样获取信息门槛显然有点高了，毕竟不是伸手即可得到。但是，对自由，对于更好的生活的追求是人类的天性，中国人也不会例外。通过网络和其他渠道，开始追求自由的人还是一天比一天多了。这种追求常常是理性的、温和的，是思想和心灵上的自我解放。但是，社会矛盾的不断积累、强化和深化，压力越来越大，谁也不能预计未来将会以怎样的方式爆发出来。说实话，我不想看到历史上一再发生过的那种山呼海啸、玉石俱焚，不希望“天街踏尽公卿骨”的历史重演。多年来，民间社会本着最大的善意和诚意，曾不停地发出自己的声音，希望权势集团顺应潮流和民意，启动政治制度层面的改革，化解矛盾，释放压力，确立和平转型的方向。而官方试图以不变应万变，一直拒绝作出回应，漠视民间的呼声。今天，稍有良知的人都已清楚地感受到，留给这个民族的时间确实不多了。<br />
<br />
&nbsp; &nbsp; 近一个甲子的有效统治，正面临着前所未有的巨大考验，吏治的全面败坏，穷富差距导致的社会对立，通货膨胀、物价飞涨给一般人生活构成的巨大压力，旧的政治制度难以适应已经变化而且还在急剧变化中的社会，因为互联网迅速拓宽的多元舆论空间……这一切都表明，中国社会不再是过去波澜不惊的一潭死水。此刻，无论官方提出多么漂亮的口号，无论承诺什么样换汤不换药的改革，只要不是动真格的，不触及最本质的自上而下的垄断型的不受制约的政治制度，人们就不会相信，不会为之欢呼。当改革不再给普通民众带来什么，当改革不再给把一个民族带往健康良性的方向，改革实际上已经死了，人心开始思变。权势集团除了顺应，其实已没有其他的路可走，这不是他们的主观意愿可以逆转的，招募多少网警、武警，拨出多少经费都将无济于事。瓮安事件以暴烈的姿态登场，就是社会积聚的怨气、不平，在某一个社会角落的一次爆发，这一点其实官方也同意。我相信它完全是自发的，一哄而起的，是无意识的，没有预谋，没有准备，是最典型的官逼民反。瓮安不是孤立的例子，它和每天在大地各个角落发生的暴力案件，杀警察、捅城管、煤气罐冲街道办……构成了乱世将临的一幅幅图画。<br />
<br />
&nbsp; &nbsp; 6月28日突然发生的贵州瓮安事件，当地民众揭竿而起，点火烧衙门，就是一个不安、不祥的信号。尽管官方告诉我们，这不是人民起义，这是“黑恶势力”从中作梗\插手煽动所致，但是，这种一锤定音的说辞很难说服天下之人。从网络论坛、个人博客，我们可以看到，许多人压根就不相信官方公布的就已经是事件的真相。官方使尽吃奶的力气开动舆论机器，努力自圆其说，想说服全国人，收效仍很有限。更多的人开始学会用自己观察，自己思考。瓮安事件之后，是继续以不变应万变，还是以变应变，该是权势集团做决断的时候了。为防止类似瓮安这样的激烈事件在其他地方不断重复，后奥运时代，中国该拉开新的帷幕了，而不是在老舞台上唱老调子的老戏。归根到底，在一个已经变化而且还在迅速变化中的大时代，以不变应万变的如意算盘不能再翻来覆去地玩下去了，如果要为这个民族、为自己的子孙后代负责的话。<br />
<br />
2008年7月9日</div>

							
							
							
							
