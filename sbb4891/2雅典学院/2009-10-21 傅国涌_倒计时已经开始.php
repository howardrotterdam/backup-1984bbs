																											<div id="postmessage_296357" class="t_msgfont">傅国涌：五四时代是什么样的时代？<br />
<br />
作者：傅国涌&nbsp;&nbsp;<br />
<br />
 第 1 页 傅国涌：五四时代是什么样的时代？ <br />
<br />
 以往我们对“五四”的理解有很多误区，我简单地概括并列举一些：将“五四”单一化地理解成全盘反传统的新文化运动；将“五四”政治化，认为“五四”使马列主义在中国出现，甚至还有一个极端的说法是“五四”导致了“文革”；将“五四”狭隘化，纯粹看作是1919年5月4日学生上街游行以及由此引发的学生和民众运动。当然还有其他一些误解，比如说“五四”是不主张私有制的，“五四”只讲思想、不讲制度，那一代知识分子没有对制度进行讨论……诸如此类，实际上跟“五四”的真实面貌都有差距。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp;&nbsp; &nbsp;那是值得生活的、非常有魅力的时代 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp;&nbsp; &nbsp;我所理解的“五四”不是1919年5月4日发生的事件，而是1919年前后那个时代，是五四时代。在我心目中，“五四”不是一个最好的时代，只是一个较好的或者次好的时代。而在整个人类的历史长河中，大凡追求最好的，收获的很可能是最坏。这个世界上从来不存在最好，只有更好的、较好的、次好的。如果这么说，“五四”就是一个值得我们生活的时代，是一个非常有魅力的时代。 <br />
&nbsp; &nbsp;&nbsp; &nbsp;我所了解的“五四”不是一个政治的“五四”，也不仅仅是一个文化的“五四”，而是一个社会的“五四”、经济的“五四”，是一个全面的、多元化的“五四”。我留意过五四时代的经济史，发现今天讲的民营经济这些东西，在五四时代是天经地义、根本不需要讲的。今天的老百姓要创办一家私营银行，恐怕还有制度的障碍，但在五四时代，任何一个中国人，只要有能力、有勇气，甚至不需要巨额资本，就可以注册一家民营银行。从1915到1925这10 年间，中国出现了大量的民营银行。最早的海归派之一陈光甫在上海创办了上海商业储蓄银行，本金只有7万元，在当时是一个非常小的银行，但很短时间内就发展成了上海举足轻重的大银行。陈光甫后来被称为“中国的摩根”，在美国、欧洲享有很高的声望。所以，抗战时国民政府才会派他和胡适到美国去借款。作为美国密歇根大学的留学生，他没有资本，也没有显赫家世，白手起家创办银行，而且做得非常成功，创立了很多在金融业界可以成为标本的做法，包括一元钱起存――今天看来十分平常的事情，在90多年前的中国却是创举。&nbsp; &nbsp;<br />
&nbsp; &nbsp;&nbsp; &nbsp;五四时代不仅可以随便办银行，更可以自由创立企业。另一个海归派、一代“棉纱大王”穆藕初，回国后就创办了一系列的纺织企业。荣德生、荣宗敬号称“纺织大王”、“面粉大王”，尽管他们创业的起点是1900年，但他们的企业真正做大、起飞也是在五四时代。那一代民营企业家几乎都在五四时代发扬光大，成为各自领域的“大佬”。其实，什么私有制、股份制、民营企业等，都不是新生事物，在五四时代早已是稀松平常的事情。谈论“五四”是不能离开这个经济背景的――那确乎是一个社会经济多元化的时代，经济的多元化促进了思想的多元化。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp;&nbsp; &nbsp;回看“五四”不能只盯着北大师生 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp;&nbsp; &nbsp;我们现在提起“五四”，往往只看到学生、知识分子，其实那个时代企业家的声音一点都不亚于知识分子――他们的声音大得很，而且更有实力。他们跟我们这个时代的企业家完全不是一回事。那个时代的企业家本质上还是读书人，无论是留学归来还是没有什么学历的，无论什么出身，他们都对国家和故土怀有一种说不出来的情感。这种情感绝不是今天“抵制家乐福”那种情感，它是一种刻骨铭心的对这块土地的连带感，他们要把自己的一切奉献给这块土地――这种爱国不是我们今天理解的画一颗五角星在QQ头像上。这些企业家今天看来更像读书人，他们的业余时间都爱看书。我发现这些做得非常成功的企业家，他们的休息时间，特别是晚上，常常是在做跟国家命运有关、跟文化有关的事，而不是跟娱乐有关的事。 <br />
&nbsp; &nbsp;&nbsp; &nbsp;回看“五四”，不能只看当时的北大教授们怎么想、怎么做，而应该把那个时代企业家想什么、做什么和北大的师生做什么联系在一起，才可看出那个时代更完整的面貌。 <br />
&nbsp; &nbsp;&nbsp; &nbsp;此外，我们不能只看北京这个政治中心在做什么，还要看看省会城市，甚至更偏远地方的人在做什么。“文学研究会”是在北大发起的，但在江苏苏州一个叫f直的小镇，有一位小学教师叶圣陶，虽然生活在小地方，但他跟北大师生的精神生活是同步的，他也是“文学研究会”11位发起人之一――因为叶圣陶有个中学同学在北大读书，他们就可以这样连在一起。而北大学生也绝对不会认为叶圣陶是小学老师，没有读过大学，跟我们混在一起做发起人会有什么不得体。那个时代的人们对这一切都感到很正常。 <br />
&nbsp; &nbsp;&nbsp; &nbsp;言论自由对五四时代的人来说是不言而喻的事情，你想创办报刊就可以创办。当然那个时代也有压制言论自由、关闭报刊的事，但关了报馆可以再开，这在法律上没有限制。结社自由在那时也是稀松平常的。周恩来们在南开有觉悟社，毛泽东们在长沙有新民学会。如果过多地把目光局限在五四时代军阀统治的维度上，只看到军阀混战，只说外国压迫中国，可能会过于片面――包括上海的租界本身也是个悖论，租界在带来治外法权、国中之国的同时，也带来了更加先进的文明，带来很多新事物，比如说“律师”就是在租界里面先示范而后影响全国的。甚至一些非常细小的事，比如街道的整齐、自来水的出现、电灯的出现都是模仿租界。中华民国作为亚洲第一个共和国，之所以在中国产生，租界也是有贡献的，上海的租界保护了许多革命党人。 <br />
&nbsp; &nbsp; “五四”时当政的徐世昌恰好是文人总统，而不是一介武夫。在徐之前，黎元洪也好，袁世凯也好，以及后面的冯国璋、段祺瑞、曹锟、张作霖这些人，确实都是军阀。虽然徐世昌是从袁世凯的幕府里走出来的，但是与拿枪者有所不同。在他当总统期间，正好是“五四”的黄金时代。文人政府当然比张作霖这些人要收敛，要更为节制。几乎家喻户晓的是，“五四”学生上街的手段是很激烈的，把赵家楼烧掉了，把章宗祥打了。这些事情在法律上都是可以讨论的，并不一定都是爱国行为了，而且曹、陆、章都是“卖国贼”这种说法也是值得讨论的事情。学生运动本身，就是“五四”上街这件事也是值得反思的。当时在天安门广场集会后，一部分比较激进的学生直接往赵家楼去，然后放火、打人。当时北大教师梁漱溟就写文章说这样做是违法的，不能在爱国的旗号下为所欲为，爱国不是万能灵药，不能打着它的旗号做法律之外、人情之外、天理之外的事。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp;&nbsp;&nbsp;那个时代才能产生真正学贯中西的人 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 回到“棉纱大王”穆藕初。他在自己创业不久、赚的钱不是很多的情况下，竟然计划拿出一笔巨款，在经过“五四”洗礼的学生当中选五个人，赞助他们赴欧美留学，这完全是用民间的方式建立基金会。穆藕初委托蔡元培（以及胡适、蒋梦麟、陶孟和这些人去辅助）物色候选人，最后选出段锡朋、周炳琳、罗家伦、汪敬熙、康白情（他们在五四运动中都有突出表现，此时却被激进的学生们讥讽为“五大臣出洋”）。这五位学生回国后在学术等领域都有建树。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;在五四时代，企业家和知识分子的关系是非常默契的。穆藕初到北京，都是跟蒋梦麟这些人游玩，而不是去跟达官贵人往来。企业家的心中有一种对知识的敬畏，对精神的敬畏。那个时代，传统的东西并没有失落，中国几千年来的文化传承到1919年前后的那几年，不仅没有被反掉，而是有非常好的传承。中国文化温情、美好的一面，在那些知识分子和企业家的身上仍保留着，西方文化中进取、向上的一面，他们身上也有了。也只有那个时代才有可能产生真正算得上学贯中西的人。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;林毓生先生有个著名观点，认为“五四”是全盘反传统，是要打倒中国的传统文化。鲁迅讲“青年人不要读中国书”，胡适讲“中国百事不如人”，钱玄同讲“废除汉字”……这是老师一辈的一些言论，学生一辈的傅斯年、罗家伦也有类似说法，但在这些片言只语后面，仔细看“五四”的代表人物，老师一辈的胡适甚至那位“只手打孔家店”的吴虞恰恰都是熟悉传统文化的人，而且一辈子都没离开传统文化。胡适晚年致力于《水经注》的版本研究，鲁迅虽然叫别人不要读中国书，尽量读外国书，但他自己读的还是中国书多。弟弟周作人说鲁迅读得最多的是野史，所以鲁迅的精神资源很大一部分还是来自中国文化。 <br />
&nbsp; &nbsp;&nbsp; &nbsp;所以，不能只看这些人说过几句貌似很激烈的话，就断定他们是全盘反传统的。事实上，当时北大的学生有主张白话文、办《新潮》的，也有主张文言文、办《国故》的，校方都给予了支持。在北大，马克思主义者可以搞自己的小社团，甚至在北大校刊上公开发表马克思主义社团的宣言，但北大讲其他主义的更多。任何主义，任何不同的思想、主张，都可以在北大同时并存。这就是蔡元培讲的“兼容并包”，它在当时不是一句空话，也不只是北大这么做。杭州第一师范学校的学生也办了类似于《新潮》的杂志《钱江》、《双十》，但同时有一个反对白话文的学生凌独见一人创办一份杂志，就叫《独见》，背后也有一些老师支持，这本刊物全部用文言文，不带标点。 <br />
&nbsp; &nbsp;&nbsp; &nbsp;这才是真实的“五四”。那时没有游行示威法，上街集会、游行是天然拥有的权利――那个时代真正是“大社会、小政府”，北洋政府比较弱，政府越弱，社会越大，社会的空间越大，所以才会产生相对开放、相对宽松的时代。 <br />
&nbsp; &nbsp;&nbsp; &nbsp;后人回眸那个时代，总感觉隐藏着什么不同的东西，其中一个不同就是中国文化的根还没有被切断。胡适们虽留洋多年，但骨子里仍是传统的中国人，他们身上有中国文化的一面。我们不能狭隘地把中国文化理解为儒家文化、“四书五经”那一套。中国文化有很多侧面，特别是我们现在忽略的东西，比如说中国文化中非常强调地域文化，每个地方的文化都独具个性魅力。胡适就是典型的徽州文化的产儿，虽然有西方文化给他的滋养。鲁迅身上带有江浙文化特别是绍兴文化的印痕，包括一些负面的因素也是绍兴文化带给他的，但是他最有魅力的一面同样是地域文化带给他的。抗战、“文革”，一轮又一轮对文化的毁灭，对文化的伤害，到最后不但切断了几千年精英文化的根，也切断了草根文化的根，切断了地域文化的根，而地域文化恰恰包含一个人安身立命最需要的东西。今天的四川人、陕西人可能差异不大，但在50年前、100年前，其差异是非常大的。地域文化如果传承、延续下来，在一定程度上可以抵抗大一统王朝的压榨，因为每个地方有它自己相对独立的文化体系，连换王朝也换不了的一套文化、习俗等。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp;&nbsp; &nbsp;不幸的是这样一个时代很快结束了 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp;&nbsp; &nbsp;如果说“五四”是全盘反传统，乃至导致了后面的“文化大革命”，这在历史的链条上很难找到印证。“五四”和“文革”之间没有任何可以连接的地方。“五四”强调的核心词之一就是独立的个人主义；五四时代最流行的主义是无政府主义，这是政府打压的对象。那些信仰无政府主义的人有一个共同特点，就是人品好，如巴金，再如三十几岁就病故的辛亥革命元老刘师复――他不要政府，干脆连姓也不要了，就叫师复。无政府主义在当时还是一股非常向上的思潮，青年毛泽东也曾被吸引。这股思潮只开花不结果，但我们不能因此否定它曾经的意义和价值。追求无政府主义的本身也是对社会公正的一种诉求，不是简单地去建立一个无政府主义的天堂。五四时代，政治看上去甚至有点边缘，因为社会得到一定程度的解放，人们就不太把政府说的话当回事，社会的价值评判体系并不掌握在北洋军阀手里，总统、总理、总长不代表真理。“五四”的最深的魅力就在这里――权力中心和真理中心不是合一的，而是分离的。人们跟政府之间，跟权力之间保持着一种相对游离的状态。我们还不能说那时已有了非常成熟的公民社会，但至少有相对独立的个人空间，有相对独立的民间社会，从经济上看是这样，从文化上看是这样，从政治上看也是这样。不幸的是这样一个多元的、有各种可能性的时代很快就结束了。 <br />
&nbsp; &nbsp;&nbsp; &nbsp;若概括“五四”的特征，第一个重要特征当是社会的出现，或者说社会的解放。我们现在也讲思想解放、个性解放，但最本质的解放应该是社会的解放。只有社会解放了，思想解放、个性解放才不至于成为空话。真正的解放是自己解放自己，而不是某个救世主来为你做主，来解放你。 <br />
&nbsp; &nbsp;&nbsp; &nbsp;第二个特征是语言的转换。虽然文字只是一个符号，一种表面的东西，但是它的背后是思维方式的转换。文言思维和白话思维是两个完全不同的思维，文言相对更具有贵族性，是少数人拥有的权利，而白话就是口头随意说的话。一个时代选择一种语言作为主要语言，思维方式也必会随之相应改变。 <br />
&nbsp; &nbsp;&nbsp; &nbsp;第三就是价值评判体系的多元化。“五四”之前，中国已废除了科举，但此后相当长一段时间，整个价值评判体系还停留在科举时代，还是 “学而优则仕”。只有到了五四时代，我们的价值评判体系才开始多元化。一个人上了新式学堂，留洋回来有无数不同的出路，可以去银行当职员，可以去工厂做工程师、做管理人员，也可以去学校做教师，可以做编辑、记者……不再是几千年来那种“学成文武艺，货与帝王家”的单一模式。正是这样一个时代才会产生新文学、新思潮。 <br />
&nbsp; &nbsp;&nbsp; &nbsp;整个五四时代，陈独秀创办的《新青年》很有代表性，但我们常常忽略了《新青年》之外的其他刊物，比如1917年创刊、1925年停刊的《太平洋》杂志。如果要研究中国近代史，在我看来《太平洋》的价值不亚于《新青年》。《新潮》、《新青年》不讨论制度性的问题，只关注思想文化层面的东西，《太平洋》恰恰是以制度讨论为主的政论性杂志，在这上面发表文章的学者，包括北大教授王世杰、陶孟和、周鲠生、李大钊、胡适、李四光以及李剑农等人。关于分治还是统一，联省自治还是中央集权制的讨论，当时曾经非常热闹，《太平洋》上几乎每期都有这样的文章。 <br />
&nbsp; &nbsp; 《新青年》上的随感，那些关于文化、思想的讨论和《太平洋》杂志上关于制度的讨论放在一起才构成了“五四”一代知识分子完整的思想图景：他们到底想了些什么、说了些什么、做了些什么。如果我们单独把《新青年》拿出来说这就是“五四”，那我们看到的“五四”就是片面的。我个人有一个猜测，1949年以后我们对《新青年》顶礼膜拜，把它放大到足以遮掩当时所有报刊的程度，应该跟《新青年》后来的转变有关。1920年以后《新青年》变成了一本宣传马克思列宁主义的杂志，所以它身上就天然地罩上了政治正确的光环，其他重要报刊却被慢慢地淹没、遗忘了。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 他们是为“美好的今天”而奋斗 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 历史留下的遗憾是，五四时代那种多元的思潮并存，人们可以坐下来讨论、对话的社会气氛，在“五卅”之后激荡的民族浪潮中不可能幸存下来 ――加上两大国际背景：一个是红色思潮崛起，另一个是二战――比较片面、激烈的思潮逐渐占据上风。对中国来说从第一次世界大战日本占据青岛起，不用等到 “九一八”，在这样一种民族危机的压力下，“五四”那种生活方式很难再维持下去了。所以，在“九一八”之前，胡适在学生中的影响要比鲁迅大得多，而“九一八”之后，鲁迅的受欢迎程度日趋上升。鲁迅其实从来没有抗日言论，没有写过什么抗日文章，他跟日本人关系非常密切，也是留日出身，但是他杂文里面民粹主义夹杂着一点民族主义的因素，对政府、精英的冷嘲热讽，加上他出色的文学才华，似乎代底层、边缘、弱势群体立言的平民立场，要比代表精英文化的胡适更能吸引中下层的青年学生。慢慢地在民族危亡当中，理性的声音就失去了主导权。“五四”那种多元的、开放的，试图通过和平、渐进的方式来推动中国的路径渐渐处于下风。 <br />
&nbsp; &nbsp; “五四”当时确实有军阀混战，有各种不同的力量，但那套宪法、国会、内阁的框架已经搭起来了，也是事实。“五四”虽然有很多不足（任何时代都会有不足），但至少给每个人提供了追求梦想的各种可能性。蔡元培去世后，陈独秀写了一篇文章说，“五四”没有领导人，但在言论上负主要责任的是他和蔡元培、胡适三个人。这三个人基本上代表了三个不同的方向。胡适代表一条渐进的变革之路，蔡元培更大度一些，他是讲兼容并包的，大肚能容天下难容之事，甚至能兼容左右翼。陈独秀是胡适的另一面，代表了以激进革命来改造社会的道路，当然，他到晚年也反思了，回到了没有踏上激进之路的前“五四”状态。中国最后选的是陈独秀的激进之路，陈独秀反思了，但别人沿着他的路走下去了。历史有很多的峰回路转，有很多的“之”字形，走来走去，这个历史又走回来了。 <br />
&nbsp; &nbsp; 我们现在生活的时代理应比五四时代更好――当然，更好的前提是我们愿意付出多少努力。这里，我想借用米奇尼克的两句话来定位“五四”：其一，“我们不是为美好的明天而奋斗，而是为美好的今天而奋斗!”这个说法对今天的国人来说多么新颖，但是放在“五四”时代，他们的每一天都是新的，每一天都是可以追求的。在90年前，他们“为美好的今天而奋斗”早就是现实了。其二，“为一个不完美的社会而奋斗，不是为一个完美的社会而奋斗！”宁愿为一个不完美的社会奋斗，也要比为一个虚无缥缈的完美的天堂而奋斗更好。 <br />
&nbsp; &nbsp; 地上建不起天堂，我们只能建设人间，生而为人注定只能做人间的平凡事。“五四”那一代人没有为天堂而奋斗，他们只是在实践他们自己的人间。</div>

							
							
							
							
