													<h2>傅国涌：历史只剩下了等待吗？</h2>
						
						
																											<div id="postmessage_296359" class="t_msgfont">一位朋友经常感叹，他们单位的一把手独裁、自私，凡事都从一己私利出发，只要对他个人有利的，能捞到好处的，必优先抢占，每天上班就躲在自己的办公室，什 么正事也不做，把份内之事也都安排给其他人去做，稍有不顺从，必遭他的嫉恨，立竿见影要进行打击报复，作风之专横，态度之野蛮，而且那么肆无忌惮，都令这 位正直的朋友有点忍受不了，认为在这样的领导下，自己是没有好日子过的，并常常将原因归结于这位领导的血型、性格。其实，天下乌鸦一般黑，每个单位，大大 小小，都是大同小异，都是中国版专制的缩影，与个人的性格关系不是很大，根子还是在于我们的体制，完全是从上到下建立起来的，是一个控制型的结构，上下级 之间的关系是统治与被统治、控制与被控制的关系，上级任命的单位头头是容不得不同意见、容不得反抗的，他代表的是一级权力，哪怕芝麻再小，也是自成一体， 他只对上负责，对下就可以颐指气使了，他掌握的权力范围内，他有资源分配权，他之所以尽最大可能地优先占有资源，这是他手中的权力所决定的。在这种权力机制下，即使他本性善良，也会被权力异化，逐渐变成一个冷血、自私、贪婪的人。 <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;有时候，我会劝这位朋友几句，说一切都会过去的，这种体制也总有改变的一天。他的回答让我一时无语，人的一生就那么些年，这样的权力体制一拖给你拖个几十 年、一百年，这一辈子还有什么指望？是啊，从这种落伍的权力体制过度到现代政治，起码现在还看到一点痕迹，没有权力的人活在这个社会是看不到什么出路的。<br />
<br />
&nbsp; &nbsp; 谢韬先生上个世纪50年代就担任过中国人民大学的副校长，晚年反思自己走过的道路，在他 85岁时写给一位朋友的信中，充满了悲凉和沉痛之感：<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;&quot;我们曾是真正忠心的毛派、左派、歌德派，挨整挨冤枉都仍然忠心不二的赶不走派。后来觉悟了，仍然是护党派、改革救党派，想方设法要救这个党，改善党，想 党变好，但悲哀在于逐渐发现，这个党是很难救了，积重难返了，救不了了。怎么办！？一个是转变，改弦更张，大气量，大手笔，大政策，开历史新局面，这可能 性极小，一个是自我瓦解，自我毁灭，被人民抛弃。这是历史的悲剧，人民（包括我们这一代）付出了那么多的历史代价，却换来一个历史悲剧，这更是悲剧中的悲 剧。&quot;<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;谢老先生的悲哀不是他一个人的悲哀，在他那一代知识分子中，在年轻时代满腔热血参与革命的老人中，那是一种比较普遍的现象，等到他们晚年觉醒时，一切都已晚了，大错已经铸成，悲剧已经发生。悲凉之雾，正笼罩着华夏大地，即便西门庆式的享乐也遮掩不住这种时代的气氛。<br />
<br />
&nbsp; &nbsp; 改革到目前已成为一个虚词，对于大多数普通人来说，改革的意义是虚无缥缈的，看不见、摸不着的，对于强势集团而言，改革意味着更大的利益，如何最大限度地 占有财富、占有社会资源，如何继续世代保持荣华富贵，这是强势集团全部的出发点，对他们有利的，就要不惜代价去做，对他们不利的，也同样不惜代价阻止。于 是，我们眼看着这个社会一天天地腐烂，整体上的、制度性的腐败侵蚀着社会的每个毛细血管，每个具备正常思维能力和良知尚存的人却都无能为力。民众，从来没 有一个时候想现在那样感到无力，一种深入骨髓的无力感挫伤了整个民族，使人艰于呼吸。许多时候，我们都陷入这样一种无力、无奈的状态中，思前想后，感到黑 暗的力量真是太强大了，强大得甚至还可以延续一百年。由于生命的短暂，我们很可能没有机会过上一天真正舒心的日子了。<br />
<br />
&nbsp; &nbsp; 改革和反腐一样都成为舞台上的道具，需要时伸手取来，不需要时放在一边。官场上，谁也不想启动一场风险莫测的改革，拿自己的乌纱帽开玩笑，权位比什么都重 要已成为多数官员的共识。民间社会，马铃薯式的芸芸众生早已习惯了认命，即使打着各种漂亮旗号的人，有几个不是在为自己的利益最大化而奔忙。改革，改革， 多少罪恶借汝之名而行。正是在改革、发展的堂皇名义下，一个个房地产大鳄、IT神话、行业垄断的王国登场了，他们掌握了这个国家最大多数的经济资源，他们占据了借改革之名带来的全部好处，他们的利益是与权力不受制约的前现代政治捆绑在一起的，他们没有理由支持真的改革，保持现状乃是他们的首选。他们分享着 美丽的大饼，生活在比天堂还要天堂的人间，他们当然不愿意进入没有人可以稳操胜券的市场竞争中，他们当然不愿意花更多成本去创立一个公正、公平的社会机 制，因为他们在世俗社会的成功本来就建立在不公正、不公平的基础之上，公平、公正是他们的克星。 <br />
&nbsp; &nbsp; 而今，利益的分配格局已基本完成，改革的动力已经丧失。对于强势集团尤其是居于统治地位的人，主动实行制度改革的可能性确乎不存在了，至少在表面上看不出 来。因此，我们再也看不到力图有一番作为的光绪帝，光绪帝瀛台修钟的命运，胡、赵80年代的前车之鉴让沉浮在权力舞台上的人们胆战心寒，还有谁敢于冒失去 权力的风险，去主张真正的改革？还有谁敢于拿自己的官场生命当儿戏，去思考国家民族的命运？成也败也的考量、个人得失的计算超过了一切理想的冲动，已掌握一定权力资源的人不是按理想行事，而是按现实可能性行事的。这是个理想成为嘲弄的时代，社会的变迁完全是利益的杠杆在起作用，鲁迅说，搬动一张桌子、改装 一个炉子也要流血，铁屋子如果没有人呼喊要把屋顶掀掉，是连窗户也开不出的。<br />
<br />
&nbsp; &nbsp;历史已进入这样一个阶段，那就是等待，也只剩下了等待，什么时候天才会亮？没有人知道，不会有人知道，也许很长，也许很短。</div>

							
							
							
							
