																											<div id="postmessage_293715" class="t_msgfont">六十年，装修时代梦将尽<br />
<br />
傅国涌<br />
<br />
一<br />
<br />
在鲜花、笑脸和举国媒体的众口一词中，盛典正大张旗鼓地展开。60年，一个甲子，多少的生生死死、荣荣辱辱，多少民族的苦难，多少次的浩劫都消失得干干净净，仿佛从来没有发生过，60年来，这片大陆开满了灿烂的鲜花，男男女女都生活在蜜罐之中，绝对权力充满了荣耀的光环，一切都因着它的恩赐，一切都是它的创造，它无所不能、笼罩全地，它主宰万物苍生，生杀予夺，并用一切大话、谎话、极尽好听之能事的好话装饰起来，铺天盖地，日日夜夜地重复，谎言重复一万遍，于是就自以为成了绝对真理，至少在愚昧者眼中看为真理。<br />
<br />
60年，一个王朝的60年庆典越来越近了，举世罕匹的阅兵式，用狭隘民族主义点燃起来的那种热情，都将铺开。王朝将骄傲地展示自己60年来的成功，60年来的辉煌，60年来的无往不胜，60年来的一贯正确、继续正确。在绝对权力的手里，历史真的像一团柔软的面粉，可以随意拿捏。60年来，这个王朝犯下的罪孽，这个王朝经历的曲折，那些血雨腥风，人民所遭遇的一切不幸，都被鲜花遮蔽起来，都被油彩涂抹了。其实，作为一个依靠暴力建立起来的王朝，早已走过了高歌猛进、从骨子里认定自己真理在握、代表着历史正确方向的时代，只剩下了装修，利益驱动之下的装修冲动。今日的权力集团可以称为史上最强大的装修公司，具有超级的装修能力，什么都能装修，也把装修当作了最高的政治追求。简而言之，这几乎就是一个可以用装修命名的时代，历史被精心地装修，现实正一天天地经历着装修，政治生活中满是装修，私人生活也处处在装修，人的本性经过装修，常常变得面目全非，连自己也不认识了。古老的大陆在修装中悄无声息地沦陷，我禁不住产生一个问号：装修，装修，在装修中生，也将在装修中死吗？<br />
<br />
盛典，就是要通过展示自己的高、大、全，来展示自己的无限风光，在向大众显示统治的威严和不可挑战同时，向外部世界显摆武力、国力和人多势众，极权主义的心理总是要通过这样的盛典才能安定自己的心，让自己相信自己的强大，相信自己的稳定，相信自己永远不会垮下。总之，就是要不断以这样的仪式来自我确认，证明权力的在手。这样的盛典说到底只是一个装修工程，或者是一次小小的自慰，面朝世界的又一次意淫罢了。<br />
<br />
60年，很长，也很短。对于任何个体生命来说，60年几乎就是接近一生了，60年，一个不遵从文明规则的统治集团可以犯下多少罪行，可以奴役多少生灵，可以糟蹋多少山河，可以毁坏多少文明，可以将一个庞大的民族翻几次烙饼。在漫长的编年史中，60年有时只能算一个零头，60年确乎太短暂了，如果没有足以引为安慰的制度建设，没有给生活在这块土地上的人们带来真实的幸福，没有值得在未来夸耀的创造，在文明的尺度之下，60年完全可能只是一支插曲，一个过渡时代而已。历史之所以令人敬畏，就是它归根结底不是掌握至高权力的人任意书写的，一个腐败堕落的时代即使装修得多么漂亮，在历史中也只能是一堆粪土，它将被一层层揭穿，让后人看见其中的糜烂和平庸。历史的审判也许来得太晚了，在一个资讯如此现代化的时代，在一个民族再也不可能关起门来自我陶醉的时代，用不着等待历史事后的审判，就在权力致力于装修的同时，我们也可以写下自己对这个时代的真实感受，洞穿一切鲜花、谎话和大话，在可诅咒的地方击穿这个可诅咒的时代。<br />
<br />
60年来，不断地有人这样做了，他们留下的声音就是最有力的见证，也是提交给历史法庭的最重要的证词。这些声音证明了一个民族的生命并不依附于王朝的生命，一个民族的生生不息乃是因为有这样的人，他们不屈从于骄横的权杖，他们用自己的眼睛看，用自己的耳朵听，用自己的脑袋思考，他们在鲜花中看见骷髅，在盛世中看见朽坏，在不可一世的宏大盛典中看见衰微和凄凉。更重要的是他们总在为这个民族寻出路，知其不可为而为之，即便在黑暗中也要袒露内心真实的亮光。<br />
<br />
二<br />
<br />
1959年，红色王朝10周年，世人看到的是有盛大的阅兵式，许多宏大建筑的献礼，遍地的饥馑，饿死人的人祸却在暗地里悄悄蔓延。这一年，一位曾真诚相信共产党的许诺、与共产党携手打天下的民主党派要人在香港出版《风暴十年――中国红色政权的真面貌》。他叫周鲸文，曾是东北大学校长，中国民主同盟中常委、副秘书长、东北总支部主委，他是1948年12月毛泽东、周恩来电召北上共商国是的8个人之一，其他7人分别是李济深、沈钧儒、章伯钧、马叙伦、陈其尤、郭沫若、茅盾，可见他那时在民主党派中的影响力。那次唯他一人没有应召，直到1949年3月才随民盟总部北上。他出席开国盛典，担任政务院政治法律委员会委员。在京8年，他的亲身经历使他做出了出走香港的决定，并写下这本书“向历史作证”。此书一经出版，就受到欢迎，半年内重版，而且出了日文版和英文版，受到海内外的重视。<br />
<br />
周鲸文这本38万字的回忆录，将红色王朝第一个10年的真实内幕首次呈现在世界面前。他目睹了土改、镇压反革命、抗美援朝、三反五反、思想改造、司法改革、贯彻婚姻法、反胡风、肃清反革命……一个运动接一个运动，每个运动几乎都浸透着血和泪，特别是他参加的司法改革运动，更使他看清共产党的本质。独占经济制度造成的铺张浪费和生产上的低效，特权阶级的奢侈腐化的生活服务，以及带给国人的灾难，都令他惊心。“人民公社”把善良的农民剥夺地一无所有，这是有史以来登峰造极的一幕。统一思想、指鹿为马和焚书坑儒带给文化上的浩劫，实际上没有等到“文革”，在第一个10年就已经展开。作为民盟的重要负责人，他与毛泽东、周恩来等人都有直接接触，对于他们如何利用统一战线的法宝，花言巧语骗取民主党派上船，与他们共建所谓“联合政府”这一幕看得尤其清楚。<br />
<br />
就在1949年政协会议期间，在少数人聚谈，说到选举中华人民共和国主席问题时，江山在望、毫无悬念的毛泽东说：“在选票上每个人有自由选举权，在选票上圈定什么人都可以，不过他抹了不想选的人名以后，最好别写西门大官人。”在场的人因此纷纷夸冒幽默。就在这次会上讨论通过了具有临时宪法性质的《共同纲领》，其实所谓讨论，无非是在文字上的“之乎者也”之间，文本的内容都是不许删动的。会上代表发言，也都是先拟好，然后交大会主席团看过批准。共产党称这之为“新式民主”，特点是“背后协商”，具有特别的优越性。政协会议开过之后，毛泽东被拥上大位，那么多民主党派似乎太多了，他想取消一些， 剩下民盟、民革、民建三个，计划将救国会、农工民主党、民主促进会和九三学社取消，合并到民盟，将致公党合并到民革。“后来毛泽东一想，这样合并于他一损，外边一定说他过河拆桥，拉完磨杀驴，因之他说：‘既然开销差不多，为何不多挂几个招牌，看来也火热。’”与那些转眼烟云的口号、文件不同，这些细节将永远活在历史的深处。从那时起，怀抱一脑子民主梦参加“联合政府”的周鲸文开始渐渐清醒，在未来的年月里，他进一步看清楚：“党是光荣、利禄、权力集于一身的，是一切私人利益的源泉；于是在红色政权下的人们，有相当数量的人像进党，而且是入了党。作了党员就一登龙门声价十倍，便处处有利可图，有权可施，有气可吐。”他于是选择逃离“虎口”，并把自己知道的这些写出来。<br />
<br />
三<br />
<br />
1979年，以“敢言”闻名的记者陆铿，应胡菊人之约，为《明报月刊》的“中共建政三十年专辑”写下一篇万言长文《三十年大梦将醒乎》。此文被称为“三十年来反共最恶毒的一篇文章”，一经刊出，即引起轰动，台湾方面认为是了解大陆最有说服力之作，军方及时印发给军官阅读，美国国务院都来打听作者是谁。当时，陆铿初到香港，家人都还留在大陆，心有余悸，不敢用真名，署名“陈棘荪”。即使化名，他还是担心因此遭打击报复。结果传来消息，此文被邓小平看到，金口玉言说：“态度是好的，观点是错的。”陆铿回忆录回首生平，对这一幕记忆犹深。<br />
<br />
文章说，三十年的岁月，两个字就可以概括：“血”和“泪”。“为什么共产党还不垮台呢？这是因为当前大陆上还没有一个成气候的政治力量，更不用说可与中共抗衡的政党了。……另外一点，不可忽视的，在大陆还有不少人对邓小平存着一定的希望。”陆铿对于镇压民主墙运动深感失望，“北京民主墙这把火，是大陆这个弥漫着冷漠、麻木、不信任的社会主义沙漠中难得找到的一小块绿洲。……是中华民族的尊严与自信尚未被毛泽东毁灭的一点象征。……这难道不正是邓大人所应该珍视的吗？”他毫不讳言地指出邓的局限：“邓小平虽然早年在法国留过学，吸收了一些民主自由思想，但他到底还是共产党人，马克思列宁主义那一套东西，渗透了他的灵魂。对于毛泽东的胡搞一气，他是反对的，但对于共产党的一套，他还要坚持。”邓在1989年的抉择可以为他的判断做一个最有力的注脚。<br />
<br />
陆铿以一个新闻记者的眼光，不仅对当时中国面临的问题，特别是“走后门”现象，和只求保官位、为自身牟利的庞大干部集团，做了深入和清晰的分析，而且看穿了中国问题的本质，“其实最主要的矛盾还是广大人民群众要求变革和中共坚持四个原则的矛盾。即民主和专制或曰独裁的矛盾。这一主要矛盾将支配今后大陆的形势。矛盾的主要方面则是中共领导层。随着中共领导层对毛泽东思想的坚持和灵活运用，矛盾将时而尖锐，时而缓和。”这一判断，时隔三十年仍不失现实意义。<br />
<br />
四<br />
<br />
1999年10月1日，76岁的李慎之写下他一生的欢乐与痛苦，希望与失望，以及对历史的一点卑微的祈求，就是实行民主。他希望掌权者主动推行政治改革，学习蒋经国，开放报禁、党禁，在市场经济之外引入议会民主，他认为条件已经“烂熟”，也是中国最理想的前途。<br />
<br />
此时，红色王朝已经褪色，如果说50年前周鲸文出版《风暴十年》时，那还是一个鲜红欲滴的时代，到30年前陆铿写下《三十年大梦将醒乎》时，红色依然但已渐渐干枯，等到10年前李慎之的《风雨苍黄五十年》一纸风行，红色已变成了灰色，王朝所标榜的“主义”已是挂羊头、卖狗肉，实用主义成为时代最高亢的主旋律。<br />
<br />
一本书，两篇文章，穿越10年、30年、50年这三次盛典，一次次击穿了盛典的华美、堂皇和雄壮，击穿了权力用意识形态谎言和刺刀精心装饰起来的层层彩雾，击穿了皇帝的那件新衣。所以，一经问世，无不洛阳纸贵，因为人同此心，心同此理，他们说出了许多国人心中想说的话，因为没有言论自由，多数人缺乏表达的机会和勇气，而他们正好将这些郁积在人心深处的话公诸了传媒。<br />
<br />
历经多少年的风雨剥蚀，这些文字仍冷冰冰地挺立在那里，散发着青铜的幽光，一次次将一个自以为是的王朝、将那些以为拥有权力就拥有一切的人，赤裸裸地钉在历史的墙壁上。每一个读者、每一次的阅读，都是它们的复活，每一次的追念、回想，都是对王朝的一次鞭挞。这是文字的力量，更是人心的力量。这是现实的审判，这也是历史的审判。<br />
<br />
60年来，从周鲸文到陆铿到李慎之，我们可以发现，王朝10年盛典时，将真面目揭开，呼吁中国人重新举起民主旗帜的是一个民主党派人士。30年盛典时，直面现实、说出真话的是一个出身于国民党《中央日报》、在共产党监狱里熬炼过的新闻记者。他们本来就是追求民主、自由的，有这样的认知，做出这样的见证并不奇怪。等到50年盛典时，站出来的却是一生追随共产党，在这个集团里成长起来、曾居中国社科院副院长高位的李慎之，他的文章之所以具有打动人心的力量，有一个重要原因，就是他的经历，他的体验，他在这个体制内部看清了历史的脉络，哪怕他对周恩来、邓小平这些掌权者怀有好感，他的真话却更有份量，更具杀伤力和感召力。<br />
<br />
还有一点不能不注意的就是时间的变化，1959年周鲸文的书在香港出版，在海外流传，大陆的读者很难看到，完全被挡在了外面，没有在本土产生应有的影响。1979年，大陆改革开放之初，陆铿的文章在香港发表，也很难传进大陆，在最值得看到的读者那里引发巨大的共鸣。等到1999年，情况就完全不同了，借助神奇的互联网，方便的复印手段，李慎之的文章到处流传，许多老人为之泪下，许多年轻人为此激动。掌权者虽然懊恼，却也没有对他怎么样。毕竟是灰色的时代了，一个体制内的反省者，可以凭良心说真话，而且也不会因此招来莫测之祸。这是1979年、更是1959年所不能想象的。<br />
<br />
离开李慎之老人在北京的一盏孤灯之下写下《风雨苍黄五十年》之日，又是整10个年头了，李慎之已在6年前告别人世，带着未尽的心愿。他没有看到这个灰色王朝的落幕，没有看到他经过无数千回百转的人生忧患，在绞肉机里绞过，终于想明白的民主价值在中国初步确立，他没有等到这一天。<br />
<br />
值得欣慰的是，互联网在社会生活中扮演的角色越来越重要，它最终是否给这个古老国家带来命运的转机，还不能下结论，可以肯定的是，在中华民族自我解放的进程中，互联网是目前可以依赖的一个不可替代的平台，王朝早已察觉互联网是把双刃剑，所以才建立了庞大的网警，建立了试图将中国互联网变为局域网的“金盾工程”，试图以网络防火墙将自由的信息隔离在墙外。<br />
<br />
令人忧虑的是，在李慎之身后，掌握权力资源的既得利益集团进一步急剧膨胀，腐败已经渗入骨髓，在现行体制下无药可救了，贫富分化、社会矛盾日益尖锐，而既得利益集团除了捍卫既得利益的决心之外什么也无暇顾及了。面对此起彼伏不满的声音，胡萝卜、大棒齐飞，利益收买和秘密警察、镇压机器并用，通往政治改革的窄门紧紧关闭，社会的繁华表象之下处处隐藏着危机，社会的正气一天天消解。既得利益集团绑架了这个庞大的国家，让它无法转身，只能坐等末日的来临。除了做些修修补补的装修工作，灰色王朝已经力不从心。或许有人把三十年来的经济成长归功于这个王朝，即使不计算既得利益集团竭泽而渔、杀鸡取卵的心态支配下，对生态环境的严重毁坏，那也只是王朝为社会松绑带来的自然结果，本质上还是获得经济机会的民众自身的创造。<br />
<br />
我不知道王朝的尽头在哪里，不知道这个王朝轰然倒塌的时日，但没有人能够否认这一天总要到来，早晚而已。我只知道，人比王朝更宝贵，每个有血肉的生命都比一个王朝的生命更宝贵，王朝的这一页注定要翻过去，王朝的时代必然让位于人的时代，以选票而不是子弹来定胜负，人权高于党权，这是普世文明的法则，中国也不可能例外，拖只能拖一时，不可能拖永久。我相信，一个已进入装修时代的王朝，它的梦将要做尽。盛典的表演就让它去表演吧，戏总要落幕的，没有一场戏可以永远地演下去，没有一个演员会永远站在舞台上，总有曲终人散时，就像梦总要醒的，倒计时已经开始。</div>

							
							
							
							
