<strong>老乐：海外华人的阴险爱国及其他二三样</strong><br />
<br />
海外华人的爱国有三种样子，一是阴险爱国，二是表皮爱国，三是骨子里爱国。<br />
<br />
什么叫阴险爱国？不好讲，阴险是一种心态，谁会傻到把阴险的心态暴露出来？不过，有一种爱国的特征不妨一展，这个特征就是无问好歹，凡事向着祖国满脸堆笑、一口奉承、从来不说半个不。跟刁德一皮笑肉不笑不同的是，这种爱国者皮肉总动员、不打折扣。我有一位朋友，在澳洲参加过不少社团活动，接触了一些“成功人士”，也接触了一些正在跟中国做生意的华侨，有一位越南华侨说：“当初共产党骗我们，现在我们也骗共产党，共产党喜欢听好话，我们就讲好话给它听。”中国需要他们座谈、表态、上媒体时，他们都按一个调子唱，从不走调。有些人在座谈会上讲的话比中宣部还要左，以至于旁边坐的人都忍不住要用脚尖去暗暗地点一下。中国人讲投桃报李，他们得到的回报是继续跟中国做生意，逢喜庆日免费往中国观光。<br />
<br />
这样很用心地|一罐劣质酒，再掺一半水，恭恭敬敬递给中国喝的行为被称作爱国。这样的爱国，你说叫不叫阴险爱国？<br />
<br />
中国喝了这样的酒，连连说好酒！好酒！再来一杯！再来一杯！这样的行情，这样酸楚的行情，我们局外人见了都暗暗叫苦，可中国它还无其所谓，你说急不急人。这样的怪现象，其实中国它是知道的、明白的。可为什么它还要对他们纵容和笑纳呢？这就跟中国共产党的传统有关，1957年大炼钢铁，土炉土灶炼出的都是毛铁坯子，为什么这些毛铁坯子还要一律统计成钢铁产量呢，目的只有一个：在账本上现出超英赶美的数字。老毛头说：钢铁元帅升帐！孰料眨眼间工夫，元帅咽气。虽然元帅被盖棺论定了，但牛逼轰轰的大呼隆影响至今。当前的形势是，中国的市场和廉价商品很受国际欢迎，而中国的共产党及其政治制度却不受国际欢迎。这个现实很尴尬、很严峻，对于执政党的地位和尊严是一个挑战，如此，共产党它就一定要想方设法来抗衡这个局面。洋人的脑袋和嘴巴它控制不了，但华人的脑袋和嘴巴它控制得了，它可以把华人搞得一窝蜂地声援中国、赞扬中国。中国的朝廷发布文件可以通达任何有华人的地方，这是因为华人的朝廷意识很浓厚，当年，清廷把抓捕孙中山的通缉令贴到海外华埠去了，这事儿闹在今天就是国际笑话，是在别人国家行使司法权。共产党它今天不公开通缉人了，它把大量人力、财力、物力花在统战上，统战就是统一战线，讲的是统一，也就是一统的意思。一统天下，依旧是个封建王朝的思维方式和运行手法。共产党在很多事情上都小心翼翼，探虚实、查动机、明真伪，但惟有给它说好话，它绝不探虚实、查动机、明真伪，而是照单全收。这个饥不择食的局面完全是内外焦困造成的，没得办法。就像卖地的钱必须进入GDP一样，阴险爱国者也被共产党纳入赤子一族。<br />
<br />
中国被这样地爱就像大款被那样地爱一样，都是用钱砸出来的。大款们很有派头，开个豪华车，置了别墅，红颜们一拥而上，大款们百里挑十。但是，得了红颜不一定就得了知己，大款有个三病两灾，命悬一线时，裹起财产跑得最快的就是红颜们。我在国内见得多了，最后回来收尸的，往往是那个被抛弃的原配，人虽寒碜，但有一颗质朴的心。穷的时候爱上你的，那是真爱；富的时候来攀缘的，多是水货。<br />
<br />
另一种爱国不是为了捞油水，但却爱得很表皮。这样的爱国流于操作，把爱国当成一种可控形式。我们所见的诸如摇红旗、喊口号、响应、歌颂、护驾等等都是相关表现。爱国爱成一种形式，也算风潮。追风潮是我们的民族特性。而追不追爱国形式的风潮则直接检验着中国人做人的立场和态度。“你什么立场？你什么态度？”在中国，这两句话是要拿前途和命运来办交割的，在海外则是区分爱国和不爱国的界标。我们由此可以看出中国人优异的灵活性和别出心裁的对事物的理解力以及机巧之心缘于何处。<br />
<br />
表皮爱国热跟当前的国学热类似，国学热下的国人并不理解也不愿意去理解国学的精髓，他们热衷于国学的形式，诸如，恢复繁体字、穿汉服、穿对襟服、背唐诗、背四书五经、祭庙子、供祖宗、续族谱、盘活孔二爷、之乎也者矣焉哉等等，在这些人中，你拿筛子去滤一下，滤不出一点儿像样的国学精神（比如死谏，一个油锅架在大堂，下头燃起熊熊烈火，你还直不直言？）所以，当前中国的国学热是形式主义热和文字游戏热，一边倒地歌颂当今太平盛世，装作没看见架在全国各省、市、自治区的油锅和扔进油锅里的谭作人们。在这样的大背景下，爱国也爱成了一种形式，这形式就是好话爱国、遮丑爱国、举手爱国……这个形式主义因了激情而转成一股意气，形成举国上下、海内海外的意气用事，谁要胆敢揭露问题、痛斥时弊，爱国者们就要操起家伙一拥而上乱棒击出，口里呐呐曰：“你还是不是中国人？你这汉奸！”五毛们图的固然是钱，但热血贲张的愤老、愤中、愤青也不在少数。这样的爱国最是如火如荼、势不可挡。同时，履行起来也简单、易行、无风险，犹如放顺水筏子----随波逐流。<br />
<br />
古时候有很大一部分读书人都忧国忧民，那不是他们自己愿意去忧，而是社会问题纠缠着他们、给他们胸口添堵。心头忡忡，救国无门，悲愤难抑时就要跳河。我们每年吃粽子，有谁事先在心头为屈原默哀三分钟？谁想过要喂一喂在屈原身边打转转的饿鱼饿虾？哪个不是拿起粽子先口水流，然后猛一口咬下去？现在的粽子，里头有五花肉、红豆沙、枸杞，屈原在水底说：“我爱国，给子孙们爱出了一道丰厚的特色小吃。”我要说，像屈原这种寝食难安、跳河自杀的人正是骨子里爱国，透支了他的精神和情感。骨子里爱国的人爱得很难看，一幅愁眉苦脸的样子，你一走近他也跟着沉重起来，没有了食欲。所以爱国爱得很透彻、很清醒的人朋友很少，尤其没有酒肉朋友，非常孤独。骨子里爱国的人，看问题不看表皮，所以爱国也就不爱表皮。表皮的光鲜很炫目，内里的瓤子才是关键，得绝症的人在咽气之前还要显出精神和红润呢，只看表皮行吗？中国的城建速度了得，到处烟花礼炮、莺歌燕舞、门庭若市、喜气洋洋，我们就一味地歌颂、赞扬、拍马屁。那因言获罪、贪污腐败、独裁专制等社会问题就完全不能说了？一说就是别有用心了，一说就是对祖国没有感情了。一个国家、一个国家的二杆子人民就这么脆弱？<br />
<br />
西方的强大不是自吹自擂得来的，而是不断寻找问题和解决问题得来的。西方人他专门找问题、找碴子，绝不歌颂政府。政府是在民众的骂声中学乖的、是在不断的丢人现眼中成熟的。能骂和被骂、能砸鸡蛋和被砸鸡蛋是文明的表现；不能骂和不愿被骂是封建的表现。封建跟专制一体，所以还在垂死挣扎的几个专制国家都是遥相呼应、相互鼓励、结伴而行，合力抵挡各类批评和声讨。国学在教国人处事之道时，特别说到对伶牙俐齿的人要疏远，对说话难听的人要结交。要知道古人结交朋友不是喝喝茶、饮饮酒就得了，那是要肝胆相照、拔刀相助的。而伶牙俐齿的人平时好得似蜜甜，灾祸来时躲得最快，绝不挺身而出。阴险爱国者就是这样的角色。基本可以说，以后中国实现民主制度了，对共产党落井下石最快的也是这样的人。因为，他们做人没有主心骨，他们的眼中也没有是非曲直，唯有利益，谁牛靠谁。<br />
<br />
君子之交淡若水，小人之交甘若醴。哪种人是君子呢？就是自己一穷二白，还舍得得罪中国，不去揩中国的油，这样的人就是君子。中国民众并非普遍过上丰衣足食的生活，但中国政府为了活得有尊严却年复一年向海外华人机构及社团组织祭出一块块肥肉，我们海外华人要真是爱国，就要想到那是中国人民纳的血汗钱，断不能伸出舌头去舔。<br />
<br />
（2010、5、15老乐于澳洲）<br />
