<br />
冉按：拜网络之赐，频年来得遇诸般温暖的事。有毛根儿朋友数十年不见，久无音问，却有一天因为他擅于诸事不决问google，奇迹般地联系上了；有中学同学，早已去国多年，忽因在一论坛见别人转我文章，顺藤摸瓜，找到我博客，因此得续同学旧缘；更有老友在敝博潜水，平日并不“打扰”我，待我被有司软禁审讯时，即频电国内好友，殷殷垂询诸事，其关怀备至，令人感怀。世事苍茫，所来何为？人生寄一世，奄忽若l尘，这些都是甚可感念的事。今日所要推荐的文章就是殷殷关念我的老友老乐在澳洲所作。<br />
<br />
老乐本画家，但写小说特别是短篇小说写得不错（我曾荣幸地当过他的编辑），菜做得很好，人很幽默，动手能力超强，很得生活的真趣。说他十项全能，虽不能加诸冠军之名，但说他样样在行，多才多艺，那决不是夸大之辞。更为难得的是，他温润宽容，实在是个天生的消极自由主义者。不仅“岂所欲，勿施于人”他能做到，而且像我和廖胡子亦武这样臭毛病比较多、说话比较刺儿的人都是他的朋友，你不难想见他的包纳之怀。偶有人事未谐、亲谊不睦，也未见其疾颜厉色，更不可能看到他要去写“与某某绝交书”，颇有“君子绝交不出恶声”的范儿。<br />
<br />
我很庆幸，有一些在国内并没有深交的朋友，在他们出国多年后，却每每捎物寄书，常写信问候。他们回国后来见我，就说以前老觉得你批评政府太多，是不是把中国看得太过黑暗。后来到了自由国家，有了对比，才知道你批评得并不过，认可你坚守的价值观。我听后深感欣慰，不是因为他同意我的观点，而是他终于认可民主自由的普世价值，回归自然的常识和普遍人性。我跟老乐则不同，在国内时本身就是朋友，他到了国外，亦丝毫不曾损伤我们的友谊 ，这是一种很难得的、常温常新的友谊。我很难说人生得一知己的话，这话太过郑重其事，但我以为我跟老乐之间，互相是比较懂的，我很看重这样的“懂”。<br />
<br />
本来我跟老乐并不事事雷同，为人处世亦有差别，自然观念亦不可能重叠到毫发无差，世界上没有这样的人。这不，最近因为温家宝先生讲话的事，他比较“挺温”，我相对“批温”，他连发数篇“挺温”的文章到我的邮箱里，还说批评了我的朋友（如余杰），怕我不悦，希望我理解之类的话。我在想，老乐，老乐啊，要是当面的话，我可能会“骂”你，你原来还是这样小瞧我冉某人的么？我连“毛泽东思想旗帜网”曾被封，他们的言论遭打压，都替他们说话，批评官方的打压，认为言论自由大过意见的不同。我认为，对我言论之“敌”的打压，就是对我言论自由的伤害。还见不得你这样理性的“挺温”言论不曾？顺便说一句，国内外“挺温”的朋友，都自有其道理，只是我们的看法和方向有些差别。即论有此区别，也不影响我尊重胡平、崔卫平等诸多师友。<br />
<br />
今天推荐老乐这篇文章，是因其有一定的普遍性，是海外爱国主义的样板。当然老乐兄这篇文章在深度上还有欠缺，比如指出险恶爱国主义者对中共的利用，但小瞧了中共对险恶爱国主义者的利用。与险恶爱国主义者相比，中共利用他们而所获得的利益，远过于他们，这才是二者深度勾结的因由。如果只有险恶爱国主义者赢，而中共输，他们是断不干的。中共固然有不少被耍的时候，但永远不要低估他们邪恶的实用主义和对利益最大化的追求。本来要推荐老乐的《中国道德崩溃始末及重建考》一文，但我认为对海外爱国主义的分析更有吸引眼球的“时效性”，故尔特推此篇。再者我将他的标题《海外华人的阴险爱国及二三样》，更动为《海外华人的阴险爱国》更精悍而显主旨，请老乐见谅，内文里他的标题一仍其旧。至于要看老乐其他文字和绘画，请移步老乐在博讯上的“老乐文集”。 <a href="http://boxun.com/hero/laole/" target="_blank">http://boxun.com/hero/laole/</a>。2010年9月26日星期日10：06分于成都<br />
