标签： 杂谈&nbsp;&nbsp;<br />
&nbsp; &nbsp; 今天又有一起离婚案件因为一方不承认感情破裂，而继续延续了一段‘美满幸福’的婚姻生活。<br />
&nbsp; &nbsp; 我一直认为中国婚姻法里关于离婚的限制性规定非常混帐，典型的挂羊头卖狗肉，前面说婚姻自由――包括离婚结婚，后面在离婚的要求里却说非得感情破裂――这个感情破裂的决定者，居然不是当事人，而是由法官来决定。<br />
&nbsp; &nbsp; 鞋在我脚上穿着，是否合适我自己最明白，你一个外人来决定――是不是很滑稽？<br />
&nbsp; &nbsp; 目前我国的司法实践是这样的，如果没有法定事由，一般第一次都不判决离婚，只有第二次（事隔半年后）再次起诉，才可能被认定为感情确实已经破裂，那个时候才能彻底获得自由身。<br />
&nbsp; &nbsp; 这个司法实践打着稳定家庭关系的旗号，实际做着大范围侵犯人权的勾当。<br />
&nbsp; &nbsp; 离婚的双方都是成年人，不是傻子，呆子，既然婚姻自由，我可以今天结婚，明天离婚，后天复婚，你管得着吗？凭什么像小学生一样被你制约，让你上纲上线地管教，限制我的权利？<br />
&nbsp; &nbsp;我国的婚姻法必须改革，必须去掉那些含糊其词的规定，否则第一恶法的臭名这辈子都给他扣上！<br />
&nbsp; &nbsp;再说那几条一点不具备操作性的法定离婚事由：<br />
&nbsp; &nbsp;(一)重婚或有配偶者与他人同居的；什么叫同居，怎么取证？看见两人一起进出某个单元就是同居？<br />
　　(二)实施家庭暴力或虐待、遗弃家庭成员的；挨打的时候可以报警，可是有几个警察能处理打人者的，又有几个可以出庭证明的？<br />
　　(三)有赌博、吸毒等恶习屡教不改的；这些更不好举证，必须政府处理才有证据，而且处理一次，还不行，必须两次以上，因为屡教不改嘛！<br />
