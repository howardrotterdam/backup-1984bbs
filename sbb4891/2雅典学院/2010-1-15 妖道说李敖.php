																											<div id="postmessage_432622" class="t_msgfont">我在抓高级五毛那个投票帖说到李敖，我说其人话虽不中听，其实饱含深意。这里展开讲一讲。不爱听的请捂住耳朵，忍不住的骂人拍板砖我都欢迎，我绝不搞投票发动群众撵你。<br />
<br />
有人认为李敖其人亲共。我不否认其人与共有交集，比如都反蒋，都认一个中国，在一些问题上能找到共鸣。但我认为没根据说李敖认同共产意识形态，也没根据说李敖认同专制。<br />
<br />
还有人说李敖老而不尊，游戏心态造访大陆，特立独行到了为标新立异而标新立异。我就纳闷，游戏心态有啥不好，连战那一脸严肃的党棍心态好？大陆之行人家拿你当招牌，你就游戏一把耍一耍，把要说的话说出来还不用担心被封杀，不是很好吗？李敖玩世不恭是出了名的，欣不欣赏纯属你的个人口味，与李敖没啥关系。<br />
<br />
至于李敖老了，不再锋芒毕露，这倒是可能，老猫还不如小猫活泼可爱呢。不过李敖能让自己袒露私处的裸照在网上招摇过市，其他七老八十的人谁能，我想年轻的李敖也未必敢有此为。此，少要稳重老来狂，有几人做得到？<br />
<br />
李敖骨子里从来都是自由主义者，这个我决不怀疑，除非哪天秘密档案解禁，发现李敖50~60年代就入了党，成为特殊材料制成的江姐式人物，为党为台湾解放受尽国民党反动派的酷刑折磨，跳楼自杀的罗广斌感动之余九泉之下另作“红岩续集”一本。<br />
<br />
那么怎么理解李敖的大陆之行？我觉得欲理解李敖必了解李敖。李敖大陆之行的许多言行不是给大陆人听不是给大陆人看的，他是给台湾国民党和民进党的政客们看的。李敖虽大陆行，但他的情感、利益、历史纠葛依然深深地维系在台湾，一举一动最关心的不是大陆怎么看，而是台湾怎么看。如果说连战大陆行是豪赌两岸关系，李敖来大陆除文人怀旧之外，也是想抓几张好牌回去打呀。<br />
<br />
读到此处一定有人不满地说：那么你认为李敖就没对大陆民众喊话？<br />
<br />
喊啦！怎么会不喊？李敖耍小聪明，寻找大陆话语体系向大陆民众喊话，所以一口一个毛泽东怎么说，结果非但没起好作用还引起误解，得罪了中国的浅薄自由派。（当然啦，像我这样的广大自由派知识分子还是能理解他的深意的。不许笑！）。<br />
<br />
李敖大陆说出的最有价值的话是呼吁大家尊重宪法，追求宪法中公民权利的落实。这是非常狡猾的策略。宪法由当局制定，民众说：好，这个宪法我们认同。这就是共同底线。那么你制定的宪法，其中的公民权利就应该落实，我们维权争取这个，自由表达权、出版自由权、结社权，都落实了，社会离宪政就近了。这时修宪就成为现实要求，社会各界的迫切要求，转型水到渠成。<br />
<br />
你开始就不以宪法说事，不承认宪法，当局也不拿宪法当回事，双方找不到诉求点，你的一切要求都找不到基点，当局乐见其局面，因为这时权力和暴力找到了发言空间。<br />
<br />
我说的李敖深意就在于此，这老家伙国民党牢没白坐。</div>

							
							
							
							
