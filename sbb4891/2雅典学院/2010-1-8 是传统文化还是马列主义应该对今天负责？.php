													<h2>是传统文化还是马列主义应该对今天负责？</h2>
						
						
																											<div id="postmessage_420438" class="t_msgfont">作者：三户王勤 <br />
<br />
&nbsp; &nbsp; <br />
　　<br />
　　 我既往写过一篇短文，题目是《马克思主义究竟是一个什么样的主义？》。大意是以我自己的理解和观察，马克思主义尽管仍为十数亿人口大国的官方意识形态与理论基础，也还在相当一部分国人，包括知识群里享有相当的尊崇，但我却认为它是一个将人类社会导向暴力、仇恨与奴役的“馊主意”，是应该深刻揭露不断批判的反社会反人类理论。其实这样的结论，早有哈耶克、波普尔等西方贤哲作了系统论述，不用我来画蛇添足。只是这一早已得出并为世界历史所实证的结论，好像还不太为多数国人所知晓或接受。<br />
　　最近在网上细读了傅国涌先生《李慎之先生晚年的悲凉――与许良英43封通信的解读》一文，详细披露了李先生从一位当年热烈追求共产革命理想并成为共产党高官，到了晚年却蜕变为自由思想者的心路历程。李先生的大作以前读过不少，而他和许良英先生的私人通信，则将自己的思想演变历程展现得更加淋漓尽致，催人深思，让人反省，也促使我去反思自己是从什么时候开始彻底脱离马列毛共的思想桎梏：应该是在大学阶段开始，至硕士阶段完成（八九年）。我思想的轨迹，正好顺应了中国改革开放的历史潮流，从发端流向混沌，再到清晰。 <br />
　　不过，在欣赏和佩服李先生这一代人的自我反省与批判能力的同时，我现在也有不太认同李先生观点之处，那就是李先生由前半生欣赏和赞美中国传统文化，到晚年彻底否定和抛弃中国传统文化，认为传统文化大都是专制封闭的成分，对民族精神、社会制度和国家现实的影响皆为负面。在这一点上，我略有异议。对传统文化的认知，我也曾经在人生的一段时间里，持极端否定的立场，但随着人生阅历与历史视野的丰富和拓展，逐渐放弃了这种对中国传统文化的全盘否定立场。<br />
　　我的基本观点是，中国传统文化是一个相当庞杂的精神遗产，可以看作是极大的精神负担，也可以从中发掘有价值的东西；里面固然包含了专制乃至反科学反人性的成分，但也不乏有价值的思想资源，尤其是在早期的民本主义、大众审美、世俗伦理、对知识与道德的称颂等方面，有自己独特的价值，不宜一概否定。如同上天赐与千年古国的一座高山，多少年来一直遮蔽了人们前瞻的视线，也挡住了民族迈向科学民主的进程，或者说，这片山脉没有带给人们以实惠的现实资源和利益，但正是这一千百年来看上去没有任何实用价值的山脉，却组成了平衡自然生态环境的重要因素。倘若缺少了这样的元素，人类赖以生存的生态环境就会由量变到质变地逐渐恶化，甚至诱发无尽的天灾人祸。<br />
　　不错，对现代科技、现代民主政治和市场经济而言，绵延数千年的传统文化可供借鉴的实用价值实在有限，但它对民族品格和精神特质的锻造与型塑，对保持文明的传承与连续性，对人心、家庭、社会伦理以及艺术审美的薰习陶冶，却有着不可或缺的潜移默化的作用。<br />
　　既往学界和普通民众颇为流行的观点，是将中国今天的政治专制文化堕落，说成是中国传统文化禁锢与模塑的结果，认为传统文化缺乏现代科学和民主的基因，这是将传统文化这一复杂的精神遗产过于简化的线性思维结论。我坚持认为，若将今日中国政治的专制和思想的禁锢，简单地推诿与传统文化的负面作用，恰恰是一种懦弱与懒惰的做法，也正好起到为马列共产理论这一人类最为严酷的专制源头作了开脱和洗刷，而今天台湾、韩国、不丹等亚洲新兴民主国家的崛起，正好回击了这样简单的思维逻辑。根据卡尔波普尔的观点，现代自由民主社会（开放社会）的头号敌人，正是源于西方的柏拉图、黑格尔与马克思这三位曾经被东西方都捧上圣坛的所谓思想家和哲学家。正是他们的乌托邦思想，将二十世纪的人类引向了万劫不复的灾难深渊，长终而不得返（参见波普尔《历史决定论的贫困》、《开放社会及其敌人》和《二十世纪的教训》）。 <br />
　　在共产主义作为官方意识形态流行的国家，无论是苏俄，或是中国，也无论是北韩，还是红色高棉，民族的传统文化与宗教信仰，都无一例外地受到了前所未有的摧残和践踏。文革十年，这一反传统反文化的暴民思想更是登峰造极，中华传统文化几乎被铲草除根，时至今日，其反文化的遗毒仍然深刻影响着一代代后来者的价值观与精神追求。<br />
　　<br />
　　这一人类共同的迄今最为惨烈的灾难与教训，才是我们今天最应该反思、警惕与记取的。<br />
　　事实上，早已有学者如王焱等人提出，一种文化对一种民族的综合价值，不能简化为社会政治层面的实用理性价值。王焱指出：“近代中国知识分子由于价值迷失，误以政治为全能，往往将不同的价值领域统统化约到政治领域中，视政治领域为终极关切所在，而不能理解把政治作为‘用’，作为在道德上中立的用于维护个人与社会权利的一种技术和工具，结果混淆了恺撒的世界与上帝的世界，造成政治系统的极度肿胀，乃至最终导致国家对于社会文化领域的全面吞噬。这种对于政治的过事推崇，其实往往不过是全能政治下的一种意识形态立场。从一定意义上说，这种认识甚至远远落后于张之洞。”（参见王焱：《晚清宪政的先驱――张之洞与中体西用》一文）。我们可以这样说，传统文化在当下的中国，一定程度上一定范围内，起着类似宗教对世道人心的教化作用，它是一个民族文明的根基所在。<br />
　　总体而言，文化是一个复杂且关乎社会、经济制度、价值与思维体系、历史与现实资源、民风民俗等包罗万象，既渊远流长，又庞杂沉重的价值符号系统。我们既不可高估传统的现实意义，也不必如愤青们那般非理性地反传统反文化。人类代代相传遗留下来的传统文化，恰似那浩瀚无涯的大海，有着后来人取之不尽，用之不竭的价值与思想资源。是上苍所赐，智慧所本，我们理应庆幸，理应感激。当然，个人也好，政治或利益集团也罢，都可能从自己本能的或现实的需求出发，或者纯粹从个人的兴趣爱好出发，来解读历史，理解文化。从各民族或族群的传统文化中，寻找自己认同的价值资源，从而使传统文化得到不同的阐释和发挥，既是一个现实格局，也是一个永恒的主题。这或许也是我们今天仍在争论不休的价值与意义所在。</div>

							
							
							
							
