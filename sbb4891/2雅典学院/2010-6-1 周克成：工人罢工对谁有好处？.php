													<h2>周克成：工人罢工对谁有好处？</h2>
						
						
																											<div id="postmessage_650787" class="t_msgfont">人们普遍对工人罢工行为持支持态度。比如本田在华公司的工人罢工后，北京独立学者秋风先生就马上在一个邮件组中发信叫好。我们不妨看看工人罢工对谁有好处，对谁有坏处。<br />
工人罢工对两种人有好处：<br />
第一，工人罢工对于那些主动罢工的工人有好处。有些工人要提高收入、减少工作时间、阻止外人与之竞争，但单凭自己的力量又做不到这一点的时候，就希望能通过罢工来要挟雇主或者政府。在罢工的威胁下，雇主和政府往往又只能选择退让，这些以罢工来表达自己诉求的人就取得了成功。<br />
但是，工人以联合罢工的方式表达诉求、争取利益，是一种违约行为。假如对工作条件和收入不满意，可以不和雇主签订劳动合同，但既然签订了合同，就应当如实履行。当然，签了合同也是可以违约的，但那就得承担相应的违约责任，而不能认为自己违约了还理直气壮。<br />
第二，工人罢工对那些希望从社会动乱中谋求政治利益的人有好处。有些人为工人罢工叫好，并不是因为他们真的关心工人的权益，不是他们以为这样真的能如何改善工人的境况，而是他们就是喜欢看到有人上街制造麻烦。他们认为社会乱了，政权出现更迭了，他们上台争取政治利益的机会也就来了。这种人用心险恶，如果他们能成功，对社会实在看不出有什么好处。<br />
对于很多其他人来说，工人罢工对他们可就没好处了：<br />
第一，工人罢工对那些不想主动罢工的人没好处。是有很多人愿意联合起来罢工，但也有一些工人是不愿意参加罢工的，他们愿意接受目前的工资水平。但是在罢工运动下这些工人的权益常常受到罢工组织者的侵犯。如果要继续工作就会被罢工者斥之为“工贼”，严重者还可能遭受暴力攻击，连生命财产安全都受威胁。比如2008年的重庆出租车司机罢工事件，其中一些不愿参与罢工行动的出租车司机的车窗就被人打破了，很多工人迫于这种暴力威胁而不敢上路开工。对这些人而言，在那两天失去了工作赚钱的机会。<br />
第二，工人罢工对雇主没好处。无论如何，工人罢工不会对雇主有好处，有些时候，这些雇主还可能因此承受重大损失。比如薛兆丰提到的“摘葡萄工人罢工”：“摘葡萄的工人，罢工两个礼拜，可使葡萄园全年收成泡汤。”再比如航空公司面临飞机师罢工，这些航空公司的机票已经售出了，乘客已经到了，飞机已经准备好了，但就因为飞机师罢工而只能停运，这样这些航空公司不但损失了之前准备的一大笔成本，还得为违约行为给乘客高额赔偿。<br />
第三，工人罢工对消费者没好处。罢工造成的生产停滞当然不会对消费者有什么好处。上面举到的例子，摘葡萄工人罢工造成葡萄供应量减少，吃葡萄或喝葡萄酒的消费者就得面临更加紧缺的供应；飞机师罢工，计划出行的人就只能突然改变行程，而由此可能造成的各种损失可能很大；或如果一个城市的地铁司机全部罢工，几十万以地铁为主要交通工具的上班族就得迟到甚至旷工。<br />
第四，工人罢工对其他竞争者没好处。上面提到，有些工人罢工的诉求就是阻止外人与之竞争，如果这些罢工者的诉求得到满足，自然是对那些想进入该行业的人没有什么好处，和那些被迫罢工的人一样，他们失去了工作的机会。同样是重庆出租车司机罢工的例子，当时他们的一个诉求就是要求政府打击黑车，原因就是这些黑车抢了他们的生意。然而，这些黑车的存在其实是对市场有好处的，他们不但方便了市民出行，还给自己找了一条谋生之路，很可惜，罢工者的诉求得到了政府的响应，政府答应进一步打击黑车。<br />
总之，到现在为止，就我自己而言，只看到罢工对一部分工人有好处，这个好处又是建立在违约的基础之上的。坏处却有好几个，而这些蒙受坏处的人，比如雇主、比如竞争者其实都是挺无辜的。</div>

							
							
							
							
