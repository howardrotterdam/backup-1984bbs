<a href="http://blog.sina.com.cn/s/blog_54b366670100hjk0.html" target="_blank">http://blog.sina.com.cn/s/blog_54b366670100hjk0.html</a><br />
<br />
张宏良<br />
<br />
如果说，基因图谱绘制完成是人类科学史上最伟大的一天，那么，中国科学界在转基因主粮问题上的表现则是人类科学史最黑暗的一页；如果说，布鲁诺走上火刑柱是科学家忠于科学精神的辉煌标志，那么，中国科学家在转基因主粮上的表现，则是科学家埋葬科学精神的肮脏墓碑。<br />
<br />
在转基因主粮这个事关中华民族种族安全的问题上，这个事关13亿中国人民生命安全和子孙延续的问题上，100多个生物学家和人大代表中10个生物学院士，却彻底丧失了最起码的科学精神、科学良心、科学态度和科学立场――无一人直截了当地说支持种植转基因主粮，却又无一人不是立场鲜明地支持转基因主粮。所有这些科学家全都采取了中国官场上老练官僚独有的油滑作风：既语言暧昧又态度明确，既含糊其辞又立场鲜明；一致要求和建议种植转基因主粮，又没有一个字提到转基因主粮；支持立场十分明确没有丝毫疑义，但在语言上又同样没有留下丝毫把柄。即便将来转基因主粮造成再大恶果，哪怕是造成中华民族灭绝，他们也没有任何责任，到时候，他们所有人都可以堂堂正正地辩解说：我从来没有说支持种植转基因主粮，我只是说要发展转基因技术。<br />
<br />
争论的焦点，明明是转基因主粮，却被恶意偷换成为转基因技术，虽然把转基因主粮偷换成为转基因技术的，是美国操纵的那些所谓民主派，而非科学家，但是，所有科学家却有意识地接受了这种概念偷换，在转基因主粮和转基因技术之间划了等号，以此来蒙骗天理、蒙骗公众、蒙骗中央，更是蒙骗了科学良心。<br />
<br />
中国这100多个生物学家和人大代表中10个生物学院士让科学界蒙羞千古的表现，将会永远成为人类科学史上最丑陋最黑暗的一页。<br />
<br />
在任何一个科学问题上，都可以选择支持或者反对，但是不能不留丝毫痕迹的支持或者反对。政治家不留丝毫痕迹地支持或者反对，是一种高超的政治智慧和政治技巧，但是作为科学家，不留丝毫痕迹地支持或者反对，则是丧失科学精神和科学良心的耻辱行为。<br />
<br />
作为人类科学史上最丑陋最黑暗的一页，不仅仅表现在这些科学家油滑的官场作风上，还表现在这些科学家支持转基因主粮的理由，根本不是科学问题，而全部是社会问题。那些大肆妖魔化转基因主粮反对者的根据之一，就是支持种植转基因主粮的都是自然科学家，而反对种植转基因主粮的都是研究社会科学的，根本没有资格谈论转基因主粮，由此得出了反对者十分无知的无耻结论。之所以说这是一个无耻结论，就在于支持种植转基因主粮的那些自然科学家，所列举的理由却无一是科学问题，而全部是社会问题，全部是社会科学领域中的问题，甚至是社会科学领域中最高层次的国家和世界发展战略问题。如同美国操纵的那些民主派讥讽转基因主粮的反对者不了解生物学知识一样，这些支持转基因主粮的科学家同样不了解社会科学领域的问题。可是，他们却采用与自然科学完全不同的社会科学领域的问题来证明一个自然科学问题，同时又以傲慢态度拒绝社会科学的质询，这种荒谬做法本身就已经完全丧失了科学性质，把科学变成了彻头彻尾的市井无赖，这不能不说是人类科学史上最无耻最卑劣最荒谬最丑陋最黑暗的一页，是科学史上永远洗刷不掉的奇耻大辱！<br />
<br />
虽然所有这些科学家已丧失了最起码的科学精神、科学良心、科学态度和科学立场，但是他们却代表着中国科学界的声音，正是他们这种不留丝毫支持痕迹的支持态度，致使决策层和整个社会接受了转基因主粮，把中华民族推上了前途莫测的发展道路，中华民族千万年来的正常发展道路被打断，步入了由国际生物资本所设计的人为发展道路，成为人类历史上第一个充当小白鼠的民族。而那些可怜的老百姓，却很少有人知道正在发生什么，仍然在一如既往地忙忙碌碌，忙碌着尚未被打断的繁衍生活。<br />
<br />
而与此同时，全世界其他所有国家的科学家正在带领本国人民抵制推广转基因粮食的生物资本。印度科学家带领印度人民迫使政府放弃了唯一要种植的转基因食品――茄子；欧盟科学家要求政府顶住转基因粮食的压力，对美国生物资本最大的让步，就是只允许使用转基因土豆作为工业原料，但是决不允许在欧盟范围内种植；美国医学学会已经忠告美国人民不要食用转基因食品；瑞士科学家更是带领人民采用全民公决的方式，全面禁止转基因粮食；日本科学家迫使政府只允许种植一种转基因花卉――康乃馨。<br />
<br />
中国科学家同样应该告诉饱经苦难的中国人民，转基因主粮的“非预期性”意味着什么，因为他们每一个人都知道这种“非预期性”的后果是什么。所以，他们才无一例外地选择了不留丝毫痕迹的支持态度，旨在为自己将来留好退路，为自己将来留好借口，甚至为自己子孙都留好了辩解理由，唯独没有给中华民族留下退路，没有给13亿中国人民的子孙后代留下能够回头的退路。<br />
<br />
甚至连美国孟山都公司都在帮助中国科学家安排历史退路，孟山都公司在最近发表的声明中指出，科学家只是研究转基因品种，而不能推广转基因品种，决定转基因品种商业化生产的，是政府的政策和决心。包括孟山都这些生物公司，也只是为中国政府推广转基因主粮的商业化生产，提供一种技术上的指导作用。总之，转基因主粮一旦出了问题，科学家和生物公司都没有责任，有责任的只能是中国政府。美国孟山都公司的这个态度，虽然为中国科学家洗清了责任，却让绝大多数中国人听起来脊背发凉、五内俱焚，因为责任归谁已不重要，重要的是遭受灭顶之灾的，都将肯定是中国老百姓，肯定是13亿中国人民。<br />
<br />
