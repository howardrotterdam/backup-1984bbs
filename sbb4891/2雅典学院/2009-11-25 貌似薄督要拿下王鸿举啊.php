													<h2>彭水诗案</h2>
						
						
																											<div id="postmessage_340919" class="t_msgfont">　　2006年8月15日，秦中飞写了一条名为《沁园春・彭水》的短信，这首后来给秦中飞带来牢狱之灾，并且传遍全国的词，应该说不上什么艺术性(马儿跑远，伟哥滋阴，华仔脓胞。看今日彭水，满眼瘴气，官民冲突，不可开交。城建打人，公安辱尸，竟向百姓放空炮。更哪堪，痛移民难移，徒增苦恼。官场月黑风高，抓人权财权有绝招。叹白云中学，空中楼阁，生源痛失，老师外跑。虎口宾馆，竟落虎口，留得沙沱彩虹桥。俱往矣，当痛定思痛，不要骚搞)，它只是一个喜好文墨的年轻公务员受另一条短信内容刺激，在十多分钟内的即兴发挥。 <br />
　　在后来的审讯中，秦中飞一直不承认它有公安机关所追查的深意。但它的前三句，嵌进了前任县委书记马平(调任另一县作书记后，因涉嫌受贿“数额巨大”，在秦中飞被传唤的头一天，重庆市人大许可逮捕)、现任县委书记蓝庆华和县长周伟的姓名，语含讥刺。后面的内容，则涉及到本县广受注目的政府管理、公共事务和公共事件。 <br />
　　在以后的几天里，秦中飞把这条短信用手机和QQ向多位朋友发送了。 <br />
　　8月31日，警察搜查了秦中飞办公室的电脑，没收了他的手机。他被带进公安局，当晚铐在二楼值班室。第二天，警察对秦中飞进行了两次审讯，同时搜查了他的家，查收了他的电脑，令他交出QQ号。9月1日晚，公安局决定以涉嫌“诽谤罪”对他实行刑事拘留，转移至彭水县看守所。在接下来的十多天里，警方按照短信里的句子逐条提问。 <br />
　　从9月2日开始，数十位收到过秦中飞这条信息的朋友和同事被传唤到公安机关询问，以追查短信背后的动机。 <br />
　　9月11日，正式执行逮捕。 <br />
　　9月28日，变更为取保候审。总共关押29天。 <br />
　　9月19日，李星辰(彭水籍，住重庆市)，在个人博客上记载了这桩公案。消息迅速传开，引起全国舆论哗然。 <br />
　　10月24日，县公安局对秦中飞宣布无罪开释，并表示道歉。县检察院主动提出申请国家赔偿，仅仅隔了一天，赔偿兑现。这可能创造了中国司法赔偿速度之最。 <br />
　　但就在几个星期以前，县里是把这起“诽谤案”当成大事来抓的，为此开过几次会议。有关领导公开发表意见，认为秦中飞“不是写着玩的”。而县公安局高度重视这起“诽谤案”的理由，却是它“肯定会影响社会稳定和政治稳定”。 <br />
　　然而，这个一开始进展得一帆风顺，表面上看不到任何争议的“诽谤案”，却连“被诽谤人”的自诉都没有(法律规定诽谤罪属于“不告不理”类案件)――如果有的话，应该是该短信提到的前县委书记和现任书记与县长，但他们并没有起诉，而公安机关却迅速提交了起诉意见，检察院也迅速批准了逮捕。 <br />
　　同一个案子，事实简单明确，审讯过程中，并没有发生新的案情，为什么前抓而后放？错案是如何发生的？官方思路是如何转变的？案件在体制里是如何运行的？为什么开始时县里的党政领导认识一致，公安检察如临大敌，雷厉风行，又在突然间发现“不应追究刑事责任”，并积极提出国家赔偿？既然是一个错案，又将如何追究错案责任？ <br />
　　所有这些疑问，对官方来说，仿佛并不存在。即便在“由错误走向正确”的正面意义上，官员也不愿意接受媒体采访，对民众也没有任何说明。县里的几个主要网站，如彭水农业信息网，彭水县政府公众信息网，彭水之窗，彭水人事人才网，也没有一字相关信息――一切就像从来没有发生过。 <br />
　　至于撤案的直接原因，本刊记者在跟一位不愿意接受采访的干部接触中，对方说，“市里干预了”(指重庆市，重庆实行市直管县的建制)。 <br />
　　而作为《沁园春・彭水》所挑战的一方，县主要领导们，早已实现了学历化、年轻化和异地化(指目前全国大多数地方实行的，本县籍贯不在本县做主要领导的回避惯例)，例如，书记蓝庆华是重庆大学博士，县长周伟也是大学学历，他们都是外县人，已经做过多年县主要领导，而今四十上下。</div>

							
							
							
							
