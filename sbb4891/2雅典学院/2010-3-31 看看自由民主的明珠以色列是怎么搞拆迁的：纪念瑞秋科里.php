一<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;今年的3月16日，是美国的和平活动家瑞秋科里(Rachel Corrie)遇难逝世7周年的日子。7年前的这一天，她在巴勒斯坦加沙地区的拉法赫镇进行和平活动时被以色列国防军的美国推土机碾死。她的遇难的消息在当时就没有在主要媒体上引起广泛关注，七年后的现在，她已经快被人们遗忘了。<br />
<br />
点看全图<br />
瑞秋科里（1979，4，10 C&nbsp;&nbsp;2003，3，16）<br />
<br />
二<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;科里出生在美国华盛顿州的一个中产阶级家庭，是家里三个孩子里最小的一个。高中毕业后上了本地的常绿州立学院（Evergreen State College）。据家人和朋友讲，科里从小就是一个充满爱心的人。在大学的头三年里，她作为志愿者每周探望附近的精神病患者。她还休学一年参加了华盛顿州的环境保护组织（Washington State Conservation Corps）的活动。她在大学里主修的是艺术类课程，在学习了国际政治课程后开始关注巴勒斯坦问题。在大学的第四年，她选了一个自休计划好到巴勒斯坦参加国际团结运动（International Solidarity Movement）的活动。在她去加沙之前，她还组织了一个奥林匹克镇和拉法赫镇的孩子们的笔友计划。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;科里到巴勒斯坦的时候正值以色列和巴勒斯坦冲突激化的时候，以色列国防军在加沙地带大规模袭击拉法赫镇，拆毁房屋，造成大批巴勒斯坦平民流离失所。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;科里2003年1月23日到达以色列，在不到两个月的时间里，科里积极投身于国际团结运动组织的保护巴勒斯坦平民的各种活动中。她和同事们探望难民营里的孩子，参与保护当地的水井，抗议以色列军队对加沙地带的平民袭击，抗议美国入侵伊拉克。她还和同事们穿着显眼的橙色外套，拿着扩音器以非暴力抗议和阻止以色列军队拆毁巴勒斯坦人的家园。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;她在国际团结运动的同事们称她一般为人低调，但是一旦投入和平活动总是比别人工作努力。在她从巴勒斯坦发回的信件和电邮充满了对巴勒斯坦人民的同情和对以色列政府和军方的谴责。她在巴勒斯坦的短短两个月的时间里，目睹了无辜的民众的家园被以色列军警野蛮破坏，人民被驱逐进难民营。她在给她母亲的电邮中写道：“根据我的观察，绝大多数的巴勒斯坦人采用的是甘地式的非暴力抵抗。”而她和国际团结运动的同事在加沙从事的活动，也主要是甘地式或马丁路德金式的非暴力抗议示威。她死后，她生前从巴勒斯坦发回的邮件被整理成书《我叫瑞秋科里》（My Name is Rachel Corrie）。耶鲁大学的教授David Bromwich仔细阅读了科里的邮件后认为科里对甘地和马丁路德金的非暴力抵抗方法有深入的研究。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;2003年3月16日下午，她和同事们在拉法赫镇试图阻止以色列国防军的推土机铲除巴勒斯坦药剂师Samir Nasrallah的家。据她的同事们描述，科里当时身穿带有荧光标志的醒目的橙色工作服，手拿扩音器，在推土机和房子之间向推土机驾驶员叫喊抗议。推土机停顿了一会儿，同事们说驾驶员应该是看到了科里的，然后就放下推斗向前推进，很快就把科里撞倒。在现场的同事们纷纷挥舞衣服向推土机示意，并且拿喇叭喊话，但是推土机履带碾过了科里，向前推倒了房子的一角，然后又后退再碾过科里才停了下来。同伴们冲上前去，科里的身体已经被履带压入土里，当时还有呼吸，她在被送往巴勒斯坦医院后死去。<br />
<br />
点看全图<br />
科里在以色列推土机前抗议，摄于事故发生前约1小时<br />
<br />
点看全图<br />
科里被推土机碾压后，摄于2003年3月16日16时45分<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;肇事的推土机是美国专为以色列生产的装甲推土机（Caterpillar D9R armored bulldozer），是作为美国的援助的一部分提供给以色列国防军的。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;当一个妙龄女子转辗于文明人所发明的装甲推土机的履带的时候，“这是怎样的一个惊心动魄的伟大呵！”<br />
<br />
三<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;科里的逝世的日子，和鲁迅先生纪念的刘和珍君遇难的日子（3月18日）非常的接近。虽然两位女性逝于不同的世纪，不同的国家，但是这两个悲剧却又不乏相似之处。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;科里死后，以色列军方迅速进行了“调查”，官方的结论称科里的死是一个“令人遗憾的事故”，事故的原因是推土机驾驶员没有看见科里，并且论证了这种美国生产的推土机的驾驶室的视野是如何的狭窄。这个结论被西方主流媒体广泛采用，包括《纽约时报》。似乎自由民主国家的先进推土机的视野比专制独裁国家的老式坦克车的视野差是理所应该的。有消息说这个驾驶员是俄罗斯犹太人移民，那意思是即使是驾驶员的错误，那也是俄罗斯人的错误，从另一个邪恶国家来的人。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;随后以色列政府拒绝了来自多个人权组织和民间的对科里的事故进行独立的调查的要求，只是由国防部进行了调查。几个月后，以色列军方发言人置数位目击证人的证词和现场照片于不顾，坚称科里不是被以军的推土机碾死的，并且拒绝承担科里的死亡的有关责任。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;“但是中外的杀人者却居然昂起头来，不知道个个脸上有着血污。”<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;“但接着就有流言，说她们是受人利用的。”<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;西方的主流媒体只是在随后两天进行了轻描淡写地作了报道。而她的死之所以成为新闻，也是得益于她的美国人的身份。我是在《纽约时报》的一个小角落里读到的消息。据纽约时报报道，当时科里和其他的国际团结运动的成员充作人体盾牌。更有甚者，有的媒体则直接称加沙的巴勒斯坦人是恐怖分子，科里是受恐怖分子利用，她所保护的巴勒斯坦医生的住宅实际上是恐怖分子用来偷运武器的隧道。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;“流言家竟至如此之下劣。”<br />
<br />
四<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;科里遇难七天之后，19岁的美国陆军列兵杰西卡林奇在伊拉克被俘虏，旋即被解救，她的经历被美国的主流媒体广泛报道。当美国发表宣布林奇被海豹突击队成功解救后，这个来自西弗吉尼亚州巴勒斯坦市的女孩一下子成为美国的英雄。写着“美国爱杰西卡”的T恤，标语到处都是。白宫发言人说布什总统对林奇的获救“充满了喜悦。”（&quot;full of joy for Jessica Lynch.&quot;）来自西弗吉尼亚的参议员洛克菲勒（Jay Rockefeller）在国会说：“营救杰西卡林奇体现了美国的一个核心价值观：我们关心我们的人民。（We take care of our people.）”<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;可是瑞秋科里呢？美国的政客们关心过她么？美国华盛顿州的议员Brian Baird在国会提案要求美国政府对科里之死展开独立公正全面的调查，但是这个提案在美国国会如石沉大海。以色列政府对科里的事故进行了不公开的内部调查。这份调查报告没有被公布，只是对美国驻以色列大使馆透露了部分章节的内容了事。对于这样的调查和结论，美国政府没有任何异议。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;几个月之后，营救林奇的真相渐渐流出。林奇在被俘前没有过抵抗，而且她在伊拉克陆军医院受到了良好的救治。她被海豹突击队的“解救”实际上是被导演的，当时军方已经知道医院没有伊拉克军队，而且还有伊拉克医生留下来伴陪。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;自由民主的媒体万岁。<br />
<br />
五<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;科里死后，巴勒斯坦人民为她举行了隆重的纪念活动。在加沙人民为科里举行的葬礼上，她的棺木覆盖着美国国旗，这是那些自由民主的人士在这片充满战乱的土地上一辈子都享受不到的待遇。科里的死让巴勒斯坦人民认识到了一部分美国人民的善良。阿拉法特对科里的父母说科里不仅是你们的女儿，而且也是巴勒斯坦人民的女儿。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;虽然主流世界的主流媒体已经遗忘了科里，但是在巴勒斯坦，还有很多新生的女孩被命名为瑞秋。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;谨以此文纪念瑞秋科里。<br />
