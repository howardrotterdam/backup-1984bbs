													<h2>我看余杰与刘晓波</h2>
						
						
																											<div id="postmessage_837445" class="t_msgfont">芦笛 <br />
<br />
<br />
若迷网友转贴了余杰《刘晓波的道路就是胡适的道路》一文，要我写点读后感。我知道，他是想让我评评刘晓波和余杰这两位他很看重的角儿，巴不得我说出他想听的话来。这可真是难事，盖我还真没看过这两位多少作品，只能硬着头皮写命题作文，谈点粗浅印象。 <br />
<br />
过去某大儒曾诽谤我，说我蓄意打击中国的民主人士，开出了一长串名人单子来。据说那些人都被我“辱骂”过，其中就有刘晓波、王怡和余杰。我当场指出，我只骂过余杰，从未骂过刘、王，请他拿出证据来，否则向我道歉。那大儒自然不会道歉，照例是用天下无双的厚脸皮顶过去，大概这是当代儒家的基本功吧，这也不在话下。 <br />
<br />
不过我对余杰确实没什么好印象。看过他的几篇东西，但觉空空如也，其中没一句话不是老生常谈，毫无自己的思索所得，甚至没有自己的话，遑论自己的风格。此所以我要说他是“空货仓”、“毫无内存的绣花枕头”。当然，他文笔尚称通顺，可也就是通顺而已，与方舟子的文字水平差不多。如今随便哪个文学青年都能做到这点。无论是从思想上来看，还是从文学水平来看，此人都不是什么“家”，连学者都算不上。奇怪的是评论界还把他称为“中国思想界的黑马”。不过这也不算过誉：中国原无思想界，本来无一物，何处惹尘埃？ <br />
<br />
余杰真正让我看不上的，还是作秀意识太浓烈，出名欲望太强烈。如果我的模糊印象不错，他首先轰动文坛，靠的是《余秋雨，你为何不忏悔》，让我觉得很不舒服。 <br />
<br />
我也讨厌老余，觉得那家伙是个“山丹丹花开浓艳艳”的刘姥姥式村妪，整个拿浓妆艳抹当丽质，以矫揉造作为俊逸。我自己也写过《余秋雨，你为何不脸红》，但那批判的乃是他的没文化和学术道德的阙如，并非攻击人品。 <br />
<br />
余杰那烂文字可就完全是两回事了，那完全是我在文革中见惯的以“抛档案”为“论据”的道德批判大字报，透出姚文元的浓烈气息。最令我反感的还是那题目：忏悔本来是个人的事，只能责己，岂能用于“感情讹诈”？这与绑匪何异？ <br />
<br />
小青年竟会无师自通地掌握文革基本功，令我十分惊诧。不幸的是此人就是精通这套。后来又看到他诋毁冯友兰，走的还是那轻车熟路：指责前辈知识分子无骨，不敢起来反抗共党，反而助纣为虐。他不但拿川岛之死说事，甚至指责冯友兰自“解放”后毫无学术成就！这TMD完全是站着说话不腰痛。1949年以来，有哪个人文学科的学者有过任何成就？莫非所有的人都把灵魂卖给了魔鬼？ <br />
<br />
此后才听说余杰原来是钱理群的弟子，似乎都是研究鲁迅出身的。于是便豁然开朗：余杰之所以表现出与姚文元非常相似的风格，乃是因为他们的鼻祖是同一个人――鲁迅。但凡师从鲁迅的人，从余杰到方舟子到茉莉，不管他们的政治立场如何，都表现出触目的相似性：“嫉恶如仇”，偏激，擅长道德批判，“一个也不宽恕”的战士风貌，缺乏学术素养，以及强烈的表演意识，都像样板戏主角一样大义凛然，端着鸡翅膀，鼓着“嘎几肉”，让看客替他们累得慌。 <br />
<br />
因此，似乎可以说，这些人走的都是鲁迅的路，只是没有鲁迅的恶毒阴损的骂才罢了。余杰奢谈胡适的路，却不知道那条路根本不适合他的禀赋与素质。他之所以出名，靠的也不是学胡适而是学鲁迅，而且他运气好，恰好生在一个不良不娼的黄金时代。我早在旧作中说过了，他若生在毛时代，则尚未发声便给无情地掐掉了；若是生在西方，则以他那点水平，又岂能在满园繁花中露出头角来？恰好他赶在这当中，于是便如李敖一样，可以相对安全地骂独裁统治者，因此暴得大名。那名气乃是特殊时代造成的，其实是“政治增值”，当不得真，也绝无可能长存。别的不说，若小余给流放到西方来，则立即会如同已经有过的“政治作家”们一般迅速失色。 <br />
<br />
当然这不是他一个人的幸运，“独立中文笔会”的许多“作家”都是此类货色。我不相信再过四十年，有谁还会记得这些“满天繁星”。 <br />
<br />
对刘晓波，我的印象倒是极好。这倒不是因为他在思想上有什么了不起的见解，或是在学术上有过什么开拓，而是因为他的做人。诚如余杰在那篇文章中介绍过的，刘晓波原来非常偏激，89年特地从美国赶回去闹事。但到了关键时刻，他总算及时醒悟，毅然以苍生为重，与部队接洽，让广场上的学生们在千钧一发之际撤出。此后他又陷在深沉的忏悔中，反省自己的人生道路，却未因此颓废乃至沉沦，仍然保持着敏锐的良知，时时为国家前途、百姓疾苦大声疾呼，对此我非常敬佩。 <br />
<br />
在我看来，刘晓波和余杰的区别，第一当然是深度不同，第二则是诚挚度完全不一样。刘没有什么作秀意识，其文章都是心声的真诚流露，这次签发《宪章》也不是哗众取宠。余杰则不然，看一眼《我们唯有勇气和谦卑》那恶心文字就够了。在我看来，他既没有勇气，更不谦卑，完全是利用《宪章》和刘晓波被捕一事来为自己炒作，在文中处处显示他比刘晓波高明，暗示如果《宪章》是他起草，则一定会深刻得多，盖他是比庸众高明得多的基督教圣徒，云云。这种烂文字，完全是煞风景。我要是《宪章》的作者，就绝不会拉这种人签名。 <br />
<br />
现在来看那文章。刘晓波的道路当然是胡适的道路，不过这只是从和平改良的角度而言。刘晓波并不是胡适那样的大学者，这还不仅是从学术建树上看，即使光讲对西方民主制度和自由主义的一般性理解，刘晓波也远远不及胡适，余杰就更不足论了。我不知道他们的努力究竟在何等程度上帮助了中国人的启蒙，而这在我看来乃是当代中国知识分子最重大的使命。 <br />
<br />
这当然是时代造成的。民国时代造就了一大批不依附于统治者、对社会持独立批判态度的知识分子。这个群体被中共彻底摧毁了。这全民的沉痛损失，至今尚未得到恢复。如今中国知识分子的数量可绝非当年可比，然而质量则糟到没法提。这是两方面的因素造成的，一方面是即使是在改革开放后，社会的禁锢也远远超过民国最糟糕的年月；另一方面则是我党统治彻底败坏了公民素质。不知还要过多少年，民国的学术繁荣与思想繁荣才能在中国再现，再度产生当年那一大批大学者。 <br />
<br />
从这个角度来看，刘晓波也有“政治增重”问题，这既是时代造成的全民的不幸，更是他个人的不幸。 <br />
<br />
