<br />
&nbsp; &nbsp;据一名匿名的大学生今年6月份暑假前在网上透露，由于报名参加十一游行的人数严重不足，系里竟以大三出国、奖学金来要挟学生必须参加游行，引起学生的强烈不满。<br />
<br />
&nbsp; &nbsp;“到了系办，辅导员满脸阴沉，低着嗓子很冷的责问这次报上来参加游行的人数为什么这么少。与会的干部和积极份子数十个，却鸦雀无声。我说我个人本来准备报，因为之前不知道暑假还要训练，现在知道7月12日就要开始训练，和我自己的安排冲突，所以不报。其他人估计也是这样的情况，而且有的都已经订票回家了，再让他们来不现实。辅导员接着以一种强制性的口气说我们老师整个暑假都不回去，都能做出牺牲，为什么学生不能，可以在家里休息半个月，再回来也可以啊。我心里当时觉得这个辅导员怎么一点都不人性。北京夏天超过35度，谁愿意为了那广场前的三分钟，在毒辣的太阳下训练三个月呢！现在又不是战争年代，为了一个面子工程，真是劳民伤财。”<br />
<br />
&nbsp; &nbsp;“ 后来和别的系的人谈起此事。他们都比较愤怒。系里竟以大三出国、奖学金来要挟学生必须参加游行。不游行者以后出国、奖学金都不在考虑范围内。真TM绝！游行本来就是自愿的事情。现在搞得高层给中层下指标，中层给基层下指标，基层管理者就完全把学生当做充数的工具。一层层的压迫，一层层颇具奴性的服从。国人为了面子和虚荣真是自己折磨自己。强制公民游行，此项创举简直可以去申请吉尼斯之荒诞记录了！”<br />
<br />
&nbsp; &nbsp;北京大学生网上发帖倡议罢训<br />
<br />
&nbsp; &nbsp;日前，首都经贸大学的学生因不满开学后还要继续训练，在网上发帖倡议9月3号下午进行罢训活动。<br />
<br />
&nbsp; &nbsp;在人人网上发表的罢训倡议书称，首都经贸大学在暑期训练完只让学生休息了一天就开学，开学之后每天早上7点到10点、下午放学后还要继续训练。罢训书呼吁大家9月3日下午6：30到9点进行罢训活动。<br />
<br />
&nbsp; &nbsp;有学生在人人网抱怨说，学校不但大热天让同学们穿西服训练，老师还威胁学生说如果退出，相关学科算零分；训练只要有一人出错，全体同学就得重新再来，有学生建议学校领导也来体验一下训练的滋味。<br />
<br />
