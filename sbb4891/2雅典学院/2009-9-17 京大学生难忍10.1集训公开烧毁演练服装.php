<br />
&nbsp; &nbsp;文章透露，当局耗钜资搞“六十年大阅兵”。为了安全起见，仅在北京一地，就动员了一百二十万“治安保卫”人员，二十万海、陆、空三军将士和十万<br />
警察，并花费钜资从国外进口一百九十条优良警犬，自九月十五日起，昼夜巡视，在北京城内外布防三道铜墙铁壁。文章评论称：如此的风声鹤唳，如临大敌，较之“第四代”所倡导的“和谐社会，以人为本”，无异是南辕北辙、自掴耳光！<br />
<br />
&nbsp; &nbsp;文章透露，就在距离大阅兵还不到一个月的日子里，还发生了两件令人深思的重大事件。头一个是中共官方捂也捂不住、不得不承认的乌鲁木齐“扎针事件”。<br />
<br />
&nbsp; &nbsp;另一个事件，官方至今隐瞒着，那就是北京市大学生们，不堪忍受严酷的国庆演练集训，愤而将发给他们的演练服装公开焚毁，抵制演练，并且质问当局：“你们达官贵人的子孙，享尽了六十年的余荫，又有哪一个参加了酷暑下操练演习？为什么偏偏作贱、强迫我们这些穷孩子，来歌颂你们的权贵资本主义？”<br />
<br />
&nbsp; &nbsp;据消息来源，事件发生后，中共当局惊恐万状，北京专门掌控大专院校的国保十四局已经介入，声言要严加处置卷入此次事件的学生，挑头的劳动教养，焚毁演练服者开除学籍，参与者学纪处分。<br />
<br />
&nbsp; &nbsp;文章称，六十年了，谁的中国？看起来中国依旧存续在几千年的“普天之下，莫非王土；率土之滨，莫非王臣。”看起来人民依旧是在山呼万岁、俯首帖<br />
耳。但在这虚幻的盛世之下，涌动着的是愤怒与抗争的暗潮，每年几近十万起并与时俱增的群体骚动事件就是明证。最能说明问题的是，连他们用狼奶喂大的孩子们，也开始走出欺骗与谎言的阴霾并公开与之对决了。<br />
<br />
&nbsp; &nbsp;暑假被打乱 大学生不满十一游行排练<br />
<br />
&nbsp; &nbsp;另据自由亚洲电台此前报导，因暑期被强迫参加60周年十一游行排练，打乱他们本来已经安排好的打工、实习或旅行，有不少北京大学生和中学生表示不满和愤怒。一名北大学生以诗表达愤怒：“训练场上狂晒3个月对我等求学究竟有何好处？”<br />
<br />
&nbsp; &nbsp;北京有学生在网上跟贴说，如果被强迫做某件事，即使此前自己是很乐意去做的,，强迫之下也没有了做的兴趣。<br />
<br />
&nbsp; &nbsp;对此，旅居美国的民主人士、中国80年代的大学生郑存柱表示，有北京学生对被强迫参加60周年国庆游行排练表示不满，这说明他们认识到中国现政权是个独裁的政权。<br />
<br />
&nbsp; &nbsp;“大学生经过一个系统的学习，到大学阶段能够自由地阅读一些关于政治、历史方面的书籍，然后又有一个自己的判断的话，他就知道这样的政府本身是一个专制的政府，一个政党统治的政府。那么，这样的政府、这样的价值或者这样的现状对于有理性的、能独立思考的大学生是没有吸引力的。”<br />
<br />
&nbsp; &nbsp;郑存柱同时表示，北京学生对被强迫参加十一游行排练表示不满，也反映出“June Fourth”发生二十年后的中国经济发展和社会变迁，学生关注点是希望早点毕业能找到一个好工作，要有一份好的收入，而不像以往关心政治。<br />
<br />
&nbsp; &nbsp;参加十一游行集训中的大学生中暑，被搀扶下区。（博讯网)<br />
<br />
