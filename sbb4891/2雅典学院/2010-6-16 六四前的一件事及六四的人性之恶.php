晚上七八点的时候，广播里传来北京市的通告和一些有关军队要进城的传言。我们几个刚吃了些方便面的同学商量了一下后，决定去广场支援那里的学生。然后，我们一行十几个人骑着单车，打着大旗，沿着学院路向南骑去。一路上，路旁的市民和学生不断地向我们欢呼，同我们一齐高呼口号，气氛非常热烈，但谁也没想到一场大劫正在向着北京袭来。 <br />
<br />
我们骑着单车从复兴门上了长安街。这时间正是军车已经开过去了的空隙，我们沿着长安街向东骑行，路上没遇到什么险情。当时的长安街灯光昏暗，充满了血腥和恐怖，街两旁的临时工地的围墙和薄铁皮门上布满了枪眼。当我们快到六部口的时候，一辆正燃烧着的装甲车里面的子弹还正在“霹雳啪啦”的爆着。这时候，西面开来了一些军车，我们十几个人马上和周围的人一起躲向了路边，我们十几个人也一下子失去了联系。我们其中的一个同学就是这时中了一枪。侥幸的是，他当时正半趴在另一个同学的头上，胳膊搭在中间，子弹正巧从他的头下和那个同学的头上，穿过了他的右臂，若子弹或上或下一点，那就肯定会击中他的或另外那个同学的脑袋了。 <br />
<br />
当时，我和另外一个同学小王仍没跑散，待军车过后，我俩开始小心翼翼地步行，沿长安街南侧墙跟儿向东移去，我们的生死经历就从这开始了。 <br />
<br />
这时军车断断续续地过了约半个小时。之后，我们的人群又开始聚到了一起，这时的人数已比刚才多了些，大家开始相互攀谈起来。当谈到无人得知天安门广场的情况时，大家都想冲进广场，去救那里的学生。也不知谁先打的头，我们相互挽起手来，横在长街上组成了一个人墙，然后开始唱着歌（我记得好象是国歌和国际歌等），手挽着手，向东面的天安门广场行进。当行至离大会堂西侧路约一百多米时，我们已经能看到路前方站列的军人人排了。 <br />
<br />
我们仍然继续高唱着前进，当我们行至离军人不到几十米的时候，我们已能模糊地看到前面的军人排正在平端着枪对着我们。突然，我们看到了正前方辟雳的火光，同时也听到了密集的枪声和哭叫声。前面开枪了，我们的人排一下子倒下了许多，人们一下子就散了开来。我右边挽着的一个学生，一个踉跄倒了下去，我差一点被他带倒在地。我猜他是中枪了，忙和另一个人把他架起来就往回跑。所幸，军队并没有追赶，最后我们在离军队大概二百米的地方停了下来，军队这时也停止了射击。（我清楚地记得当时的情形，当时，的确很多枪都是朝天放的，否则的话我也没命了，但我肯定当时也有不少是平射的，因为我们当时一下子就倒下了许多人。） <br />
<br />
中弹的人们很快被车推人背地架离了长安街。过了好大一会儿，人们才又聚了起来，这时我们的人又多了一些。我们又开始组成人墙，手挽着手，高唱着歌向天安门方向行进。和上次一样，当我们离军队几十米的地方，军人又开了枪，我们又被打了回来。这样反反复复约有四五次，每次我们的人数都在增加，而每次也都有中弹的被架回来。只不过，中弹人数远少于第一次的罢了。记得有一次，我旁边的又一个同学中了弹，被我和另一个人架着拖回来。我后来才看清，他的裤子大腿上被打了个黑洞，黑洞里一劲儿地向外冒血。虽然每一次前进，我和小王都是走在头排，但幸运的是，我们俩一直都没被击中。 <br />
<br />
记得最后一次行进时，天已经亮了。当行至离军队约有五十米的样子，我们已经能看清对面的军人，他们正平端着枪对着我们，我们也不敢再往前了，只稍退了一点，开始和军队人排对峙站着，中间约有五六十米的样子。 <br />
<br />
后来，我们开始高呼口号，唱国歌和国际歌等歌曲。对面的军人听到我们唱，也和我们对着呼口号和高唱歌曲。只要稍有人向前，路两旁的大棒就追打过来。因此我们中间始终与前方的军人保持着一定的距离，我们就一直这样僵持着。大约过了半个小时的样子，奇怪的事情终于发生了。正前方的军队人排突然停止了呼口号和歌唱，人排中间突然撤开了一个巨大的口子。 <br />
<br />
我和周围的人都愣住了，都不知道前方发生了什么事。正在我们发愣的时候，前面传来了“轰隆”声，这时，我们才看到，从广场方向开来了一队坦克，为首的一辆开到军队人排撤出的口子后，停了下来。接着后面又上来了很多坦克，它们开始列队，在第一对并排的坦克后，排着第二对，并且它们错列行在第一对坦克未能覆盖的空间上，然后是第三排。这样一来，整个长安街便布满了坦克，再无它们不能碾过的地带。我们看到这，才定了点心，猜测军队可能是想用坦克代替军人和我们对峙的。 <br />
<br />
然而坦克并没停下，反而开始向我们开来。这时候，也不知道那个不要命的，首先躺到了马路上，别的人看了，也跟着躺了下来。转眼已有数百人躺了下来，宽敞的长安街上黑压压地躺了一片人。当时我和小王都站在前排，看到别人都躺下了，也就一闭眼随着躺在了路中间，心想是死是活随他去吧。我转念一想，要牺牲也得牺牲得壮烈点吧，所以才又睁开了眼。当时我和小王都在第一排，我是头朝西躺的，所以能看到东面坦克开过来的情形。 <br />
<br />
坦克对着我们越开越快，马上就要碾到我们的人群了。我看得清清楚楚，当第一辆坦克马上就要压到我北侧五六米远的人群时，突然一个急煞车，急停了下来，我记得当时的马路被震得乱晃，整个坦克的上身都往前冲了一下，在离第一个人不到一公尺的地方才停住。我正前方的坦克这时离我还有十几米，也随着第一辆停了下来，接着所有的坦克都停了下来。紧接着，坦克仓门打开，军人开始向我们和路两旁扔毒气弹。霎时间，黄烟开始弥漫长安街，我和小王几乎随着所有的躺在地上的人们，一下子跳了起来，逃向了路南侧。我跳到路边，顺便往东看了一眼，当时我的肺都要气炸了，刚才与我们对峙的士兵们，看到我们狼狈鼠窜的样子，正在举枪跳跃欢呼着，我这一生都是不会忘记这批惨无人性的邪恶军人的。 <br />
<br />
毒气弹象易拉罐大小，当时吸到肚里，只觉得舌干胸闷，直想吐，我和小王开始不住地干咳。这时，我和小王看到一个刚扔到身边的毒气弹，他捂住嘴想检起来扔向坦克，我看着黄烟“噗”地一下涌了出来，小王几乎被掀翻在地。我对他喊了一声“跑吧！”我俩几乎同时开始向西跑去。这时的坦克已经形成正式队形开始向西压。由于路南道窄，毒气也太多，路北是中南海院墙，因此，我俩想斜着穿过长安街，沿着中南海院墙往西跑，这样也许会安全些。所以，我俩斜穿长安街向西北跑去。因为路上到处都是黄烟，根本看不清任何东西，我俩一下子就跑散了。也因为黄烟，我几次都差点儿撞到行驶的坦克上，坦克也差点儿撞到我。 <br />
<br />
也许我命大，或许是当过运动员跑得快的原因，我终于幸运地斜穿过了长安街，开始沿着中南海的红墙向西跑去。由于这一侧是树木花园，坦克并没压上来，所以跑起来安全多了，只是吸的毒气太多，胸腔无比地难受。当我跑过新华门时，门前密密地站了一排士兵，他们全都倒背着手直直地立着，面无表情地看着我们跑过去。我的胸部非常难受，只得蹲在地上吐了一会儿，结果什么也没吐出来。过了一会儿，我觉着好些了，看到六部口的坦克已停住了，我想找找小王，看他是否也已安全逃出，才又壮起胆子往长安街走去。 <br />
<br />
这时候，长安街上弥漫着的黄烟已经淡了一些，但稍远一点还是什么都看不见，因此谁也说不清究竟死了多少人。虽然当时的坦克还在发动着，但轰鸣声已小了很多。我隐约能听到六部口对面的哭声。我壮着胆子从最西面的坦克前绕了过去，来到了六部口十字路口的西南角。当时到处都是哭声，待我走近一看，我一下子呆了，眼泪就象流水似的一下子涌了出来，坦克附近的情形太惨了，我实在控制不住，放声大哭起来。 <br />
<br />
五个被碾死的学生横乱地躺在靠近人行道的柏油马路上。最西面的一个离人行道二米多远，头朝着西北仰面躺着，脑袋中间开了个大洞，象豆腐脑一样的白脑浆，参杂许多红血丝向前刺出一米多远。另外四个倒在他的东面更靠近人行道的地方，其中两个被压到了自行车上，和自行车黏到了一起。我和周围的人站在那里哭了一阵儿，当我看到身旁有几个学生模样的，便哭着同他们商量看是否能把这些尸体抬走，免得被军人抢去消尸灭迹。那几个学生和我一起走到邮电所西面的一个胡同，向一个住户要了一块木板，约一米多长，半米多宽，钉在一起的木板子。在周围人的帮助下，我们开始一具具地抬尸体。 <br />
<br />
当时的坦克就在旁边响着，我们流着泪，也顾不得害怕了。我已经记不清先抬的那一位了，好像是先抬的那个脑浆被压出来的学生。当时由于木板太短，他的头顶在我的肚子上，脑袋已经空了，但我的衬衣上仍然沾了一些脑浆。抬另一个内穿红背心的学生时，他的一只右臂和上身只联了几丝肉，基本上已经掉了下来，红背心和肉绞在了一起，大腿上的五花肉也翻了出来，我拿着他的右臂放到他的肚子上，然后把他抬了出去。还有两个学生已经和自行车碾到了一起，我们费了好大劲儿才将一个和自行车分开。而另一个被压得自行车的脚蹬子刺进了胸腔，我们实在无法将尸体和车子分开，只好连同压扁的自行车一起抬了出去。 <br />
<br />
记得我们抬最后一个尸体的时候，从长安街西面又开来一批坦克。我当时正拿着木板朝东蹲着准备抬尸体，根本没注意背后的事。突然间，许多市民和学生都冲着我喊叫起来。我回头一看，一辆最边上的坦克，马上就要压到我了，我条件反射地扔下木板，跳到了人行道上。转眼这辆坦克已经驶过，停在了前方，再看一下刚才的木板，约一寸厚的木板的一角已被压得象麻片一样，我被刚才的情形吓出了一身冷汗。 <br />
<br />
最后，我们终于把五具尸体抬到了街后的一个胡同里。当时，有人建议我们检查一下死者身上是否有证件，以免一会儿军人把尸体夺走了，我们还不知道死者是谁。我们翻了一下，也没发现什么，只从一个绿背包里，翻出一个女学生的学生证（女生名字我已记不清，只记得是北京ＸＸ学院的）。尸体总放在胡同里也不行，我们正发愁时，正巧来了个好心的个体司机，他流着泪建议我们把尸体送到政法大学去，以备作为法律证据。我们听从了他的建议，把五具尸体（一个连着自行车）抬上了他的小卡车上。由于害怕大路上军人抢尸体，司机师傅带着我们沿着小胡同开了很久，才上了二环路。路上他还对我们讲述了他这一夜是如何抢运尸体，一直到现在的所见所闻。由于五具尸体是迭放在小卡车上的，车子只好开得很慢，这时的汽车前后已经跟随了许多人。 <br />
<br />
汽车上了二环路后，人们骑着自行车前后围着小卡车，护送着这辆小卡车行向政法大学，一路上护送的自行车群越聚越多，当我们到达政法大学的时候，已有数千师生在校门口迎着我们，我们的前后也跟了上千人。当我跳下汽车时，一位教授模样的老人，噙着泪水抱住了我，我就象一个在外受屈的小孩，好不容易回到家里一样，趴在他身上嚎啕大哭起来。我记得，当时整个大街上什么声音也听不见，满街都是哭泣声。 <br />
<br />
最后，一位陌生人，让我搭上他的自行车把我送到了学校。当时已经是中午十二点左右了，许多同学已在校门口等了一上午，他们多数断定我是肯定回不来了。我们一见面，一下子涌到了一堆。至于后来的尸体存放和展览的事，具体的情况我就不清楚了。只是后来听说，尸体在政法大学的一楼大厅里，停放了好几天，北京市数以万计的人目睹了这五个被碾学生的惨状。 <br />
<br />
值得一提的是，我的那个同学小王也已安全地在我之前返回了学校。我们跑散后，他也差点儿被坦克撞倒。当他跑到六部口的时候，看到一个躺在地上还活着的学生，臂膀几乎已经被碾掉了下来，已经处于昏迷状态。但惊人的是，他在小王的慌乱搀扶下，竟站了起来，并能坐上一个自行车，扶着自己的那只胳膊逃了出去。六四很久以后，一个偶然的机会，我得到了这个学生的下落。这时他已经永远地失掉了那只胳膊，也被迫离开了他所在的那所大学。 <br />
<br />
我是流着泪，写完这段回忆的。我由于害怕牵连受迫害，一直沉默着。看到“六四”事件已经十二年了，我想我应该把我亲身经历的“六四”血案的一个角落告诉大家。中共说「六四」没有死一个人是对的，因为死了好多的人。请人民千万不要忘记「六四」的死难者和没有人性的中共刽手们！</div>

							
							
							
							
