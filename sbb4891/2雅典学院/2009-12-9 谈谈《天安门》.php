													<h2>谈谈《天安门》</h2>
						
						
																											<div id="postmessage_363244" class="t_msgfont">　　　　　　　　　　　　　　谈谈《天安门》<br />
<br />
<br />
　　纪录片《天安门》由美国纪录片制作人卡玛（Carma Hinton）及其丈夫高富贵（Richard Gordon）担任导演及制片，David Carnochan担任副导演及剪辑工作；是美国公共电视台（PBS）的Frontline系列历史纪录片的一集，同时也作为单独的纪录片上映、发行和参展；由美国国家人文科学基金、福特基金会、洛克菲勒基金会及美国公共广播局资助拍摄。1995年10月在美国首映。<br />
<br />
<br />
一、<br />
<br />
　　在影片公映之前，围绕该片的争议已经非常激烈。《天安门》网站以《有关纪录影片〈天安门〉的部分影评》（<a href="http://www.tsquare.tv/chinese/film/dindex.htm" target="_blank">http://www.tsquare.tv/chinese/film/dindex.htm</a>）为题列出了主要文章的作者、出处、概要及链接，列表如下：<br />
<br />
白梦，“天安门的审判”，《北京之春》，1995年6月号（总第25期），第37页<br />
“一个曾经像特殊贵族一样在中国长大，并与中共高层有深厚渊源的美国人carma hinton，集合一些面目不清的人，并断章取义地选取资料拼凑成一部叫《天安门》的影片，明显歪曲历史事实，有着明显的政治倾向。”<br />
<br />
柴玲，“请尊重历史”，《北京之春》，1995年6月号（总第25期），第31页<br />
“一个有亲共历史的人为了牟取商业暴利推销自己的纪录片，将柴玲在运动中的一个录音断章取义，企图要标新立异。”<br />
<br />
阮铭，“怎样‘重估’历史？”《北京之春》，1995年6月号（总第25期），第34页<br />
“《联合报》夸奖这部片子是有关天安门学生运动的‘终极之作'。然而据我看来，那不过是一部用谎言剪裁历史的欺世之作。”<br />
<br />
胡平，“回首天安门――对当前争论的几点评论”，《北京之春》，1995年6月号（总第25期），第50页<br />
“一个人不能只要领袖的头衔、荣誉、权力或影响力，而拒绝承担领袖的责任。民运人士的特殊地位，常常使我们意识不到这种责任感，运动成功了，大家都夸我们；运动失败了，大家都骂政府。结果便是，无论运动是成是败，我们民运人士都感觉不到自己有什么责任要负。”<br />
<br />
封从德，“背离历史的历史总结――对当前争议的几点质疑”，《北京之春》，1995年7月号（总第26期），第32页<br />
<br />
龚小夏，“关于天安门运动的三言两语”，《北京之春》，1995年7月号（总第26期），第46页<br />
“八九年学运中，许多学生领袖倾向于不顾后果而采取激进的策略，不断地制造戏剧性事件，不断地将所有解决问题的建议――无论是来自哪一方――都斥责为阴谋。他们对八九民运的悲剧负有不容推卸的个人责任。”<br />
<br />
耶人，“海外民运跳不出共产模式”，《九十年代》，1995年7月第88页、8月第85页<br />
“柴玲‘期待流血’的谈话本极简单，但相当一批民运人士对此话题却充满恐惧，纷纷谴责影片《天安门》‘别有用心’。流亡人士是中国民主的‘独生子’，他们以共产党的思维方式反共产党，对西方民主取‘叶公好龙’的态度。”<br />
<br />
耶人，“《天安门》的挑战”，《北京之春》，1996年4月号（总第35期），第62页<br />
“海外民运人士应摆脱他们所习惯的共产党的话语体系和思维方式。在这些人的心目中，世界上非黑即白――你不同意我的观点，就属于共产党一伙。文献纪录片《天安门》所要挑战的，正是这种建立在无差别秩序之上的‘我们’与‘他们’的二分模式，尽可能多地告诉人们事实，而不是去当政治的拉拉队。”<br />
<br />
卡玛，“为什么《天安门》没有采访到柴玲？”，《天安门》，香港：明镜出版社，1997年<br />
<br />
　　95年时对《天安门》的质疑，内容上，一个是对“期待”的英文翻译，一个是“断章取义”和“前后拼接”；论点是认为影片栽赃学运，为中共开脱罪责；手段之一是对几个相关人揭老底、查出身。其中最为极端的，应属当年北大作家班的白梦，他除了认为“一个曾经像特殊贵族一样在中国长大，并与中共高层有深厚渊源的美国人carma hinton，集合一些面目不清的人，并断章取义地选取资料拼凑成一部叫《天安门》的影片，明显歪曲历史事实，有着明显的政治倾向。”，甚至认为：“如果当时真的坚持到了人大常委会的召开，或者军队中的某一将领为了顺乎民意而反戈一击，八九民运中的一些现实目标不就达成了吗？要知道，这种情况在当时是很有可能发生的。”（《天安门审判－白梦》）对这样的观点，你说什么好呢？<br />
<br />
　　有关正反两面的文章，前面给出的链接都在，网上也不难找到。有兴趣的朋友可以自去阅读。<br />
<br />
　　新一轮争论由2009年5月28日的《致记录片《天安门》制片人的公开信》发起，内容还是那些内容。《天安门》网站发表了《澄清事实：〈天安门〉制片人对〈公开信〉的回应》，并附了《公开信》全文（<a href="http://www.tsquare.tv/chinese/film/about_gate09.html" target="_blank">http://www.tsquare.tv/chinese/film/about_gate09.html</a>）。为方便翻墙困难的朋友，这两封公开信另帖发布（<a href="https://1984bbs.com/viewthread.php?tid=30758" target="_blank">https://1984bbs.com/viewthread.php?tid=30758</a>）。<br />
<br />
　　对影片的争议，焦点就是《柴玲1989年5月28日对美国记者金培力（Philip Cunningham）录影讲话》。判断谁是谁非，再简单不过了：把这个录影讲话拿来从头到尾认真看一遍。有的人坚持自己的看法，那就各存其异也就是了。我所知看过该片的人，直接的第一感受，确实是对柴玲的强烈不满，但还未见有哪个会从影片中得出屠杀的责任在柴玲而不在当局的。关于《柴玲1989年5月28日对美国记者金培力（Philip Cunningham）录影讲话》，已在《谈谈柴玲的录影讲话》中详细讨论过。同时，《澄清事实：〈天安门〉制片人对〈公开信〉的回应》中，对“关于‘期待’一词的含义和英文翻译”“关于柴玲是否留在了广场”“关于所谓‘把李禄的话安在柴玲头上’”三个问题，也分别进行了回应。<br />
<br />
<br />
二、<br />
<br />
　　之所以在十几年后双方重开争论，也许真正的原因，起于2007年在美国麻州波士顿，柴玲以及由她担任总裁的jenzabar公司对《天安门》制作人及长弓纪录片制作组（long bow group），以损害名誉和商标侵权两项罪名的控告。对“损害名誉”，美国法庭已于2008年8月裁决不予受理，但是“商标侵权”案仍在继续进行。有关该案的详细资料笔者没有找到，网络上仅见长弓纪录片制作组的呼吁书《纪念六四　兼论言论自由及其倡导者》（<a href="http://www.tsquare.tv/chinese/film/Appeal-Chinese.html" target="_blank">http://www.tsquare.tv/chinese/film/Appeal-Chinese.html</a>，同样另帖发布：<a href="https://1984bbs.com/viewthread.php?tid=30759" target="_blank">https://1984bbs.com/viewthread.php?tid=30759</a>），和一些正反两面的文章。<br />
<br />
　　下面先来看看柴玲控告的摘录（引自《纪念六四　兼论言论自由及其倡导者》）：<br />
<br />
　　“出于恶意和对共产党政府官员的同情，并一心想诋毁中国天安门民主运动的一名学生领袖柴玲，长弓制作组在其网站传播有关原告的虚假信息，从一些过时的报道中收集误导性言论，散播半真半假的消息，以期造成对尖子班、柴玲和Maginn的不良印象。为确保这些内容尽可能广泛地传播，以造成最大损害，长弓在未经授权的情况下使用受法律保护的尖子班商标，将读者引到长弓网站。后果之一是，尖子班的客户及潜在客户被误导到该网站和该网站上的诋毁性内容，由此造成对尖子班名誉的伤害和商业机遇的损失。”<br />
<br />
　　“我们获知并认为长弓的诋毁性言论出自对柴玲本人的恶意，图谋损害柴玲的声誉，以达到他们别有用心的政治目的。”<br />
<br />
　　“长弓必须申报由前述非法行为所带来的所有赢利，并用此非法获得的赢利对尖子班进行陪偿。”<br />
<br />
　　在这段不长的文字中，笔者强烈感受到的是中共党文化的语言：“出于恶意和对共产党政府官员的同情，并一心想诋毁中国天安门民主运动的一名学生领袖柴玲”，“别有用心的政治目的”――笔者感到惊诧的是：和文革几乎同时诞生的柴玲，竟然对这些文革语言如此熟悉！要知道，文革结束时柴玲刚刚满十岁。这不免令人想到人们常说的“狼奶”，确确实实，这是生活在这片土地上的所有人，在这个环境土壤和文化浸淫下，无可选择而不得不接受的唯一精神营养。<br />
<br />
　　至于具体的控告内容，则是：“传播有关原告的虚假信息，从一些过时的报道中收集误导性言论，散播半真半假的消息，以期造成对尖子班、柴玲和Maginn的不良印象。……在未经授权的情况下使用受法律保护的尖子班商标，将读者引到长弓网站。后果之一是，尖子班的客户及潜在客户被误导到该网站和该网站上的诋毁性内容，由此造成对尖子班名誉的伤害和商业机遇的损失。”<br />
<br />
　　对此，长弓回答仅仅是：“我们与尖子班的业务毫不相干，亦不存在任何商业上的竞争。”<br />
<br />
　　对于柴玲控告的“商标侵权”，相信多数人不难作出自己的判断。但，其实醉翁之意不在酒，在乎何处呢？看看诉讼的过程：<br />
<br />
　　2008年8月，法庭做出裁决，给柴玲的商标诉讼一个“提供证据的机会”。但是，直到09年3月底，长达半年，柴玲方面没有提供任何证据。<br />
<br />
　　2009年3月，柴玲提出向长弓取证，长弓按照要求提供了资料。同时长弓也要柴玲提供：卡玛“为中共效劳”的依据，尖子班遭到了怎样的损失？柴玲却一直没有提供。<br />
<br />
　　2009年5月29日，事过近三个月，柴玲突然向法庭提出“紧急提案”，说只能在保密的情况下才提供证据，并要求法庭将提供证据的时间再延长三个月。<br />
<br />
　　2009年6月4日，法官作出裁决，柴玲提案被驳回。法官同意长弓的意见，只有商业机密和不公开的客户才可以保密，并要求柴玲在6月23日前，必须提出有关卡玛“为中共效劳”的证据。<br />
<br />
　　差不多半年过去了，之后的进展没有看到报道，也希望知情的朋友能提供。但我相信柴玲提不出卡玛“为中共效劳”的证据。<br />
<br />
　　很显然，柴玲是在打一场并不抱胜诉希望的官司。没有胜诉希望，却还要坚持打，而且对控诉的证据事前没有充分准备事后又不积极收集，也没有进行伪造。目的何在呢？这可能是美国的“法制国家特色”：不是以利己为目的的欺诈，而是以损人为目的的骚扰。我有的是钱，我赔得起；官司虽然我输了，但我让你在应诉中耗尽你比我少得多的财力。<br />
<br />
　　长弓确实非常为难。只要柴玲注意不违反法律规定，你很难找到证据证明对方是“恶意诉讼”；而你如果真的反诉对方，那正好落入对方的期望，对方希望的就是不求胜负的和你“玩下去”。同时，柴玲可以随时撤诉，你为此做的所有一切，只是消耗你的财力而已。<br />
<br />
　　机关算尽太聪明，反误了卿卿性命。不知道柴玲是否精细计算过：即便长弓破产，卡玛的声望会不会因此而跌落；而你承诺捐赠的一百万对民运捐款，又能为你捞到多少资本。<br />
<br />
<br />
三、<br />
<br />
　　在95年，以及今年的争论中，“与中共高层有深厚渊源”“有亲共历史”等污水，泼向的并不止是卡玛，还有戴晴。有关卡玛和戴晴的“历史问题”笔者另文介绍。<br />
<br />
　　另外，在那场运动中有着很大影响，被柴玲和封从德高度评价的，还有李禄。<br />
<br />
　　这些，也留待另文介绍。<br />
<br />
<br />
北国游子<br />
二零零九年十二月<br />
