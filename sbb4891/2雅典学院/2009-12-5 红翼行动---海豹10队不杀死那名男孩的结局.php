我们先来看看“红翼行动”的大概经过。这是海豹10队在2005年6月进行的一次渗透侦察行动。一个四人侦察小组对一条怀疑藏有塔利班头目的村庄进行监视，他们的藏身之处被三个阿富汗牧羊人（其中一个是小男孩）闯入。抓住这三个阿富汗平民后，这四名海豹经过两票反对一票赞成一票弃权的投票，决定不灭口。虽然他们立即撤退，但得到牧羊人报警的当地塔利班也蜂涌而至。<br />
<br />
由于4名海豹所处的地势较低，无法用无线电信号呼叫增援，不得不边打边退，其中一名海豹以多次中弹最后失血过多为代价，独自爬上高地向指挥部发出求救信号，虽然叫来了“支奴干”，但却被塔利班打了下来。最近这次行动导致三名海豹的侦察兵、八名160特种航空团的机组成员和八名乘坐直升机参与救援的海豹阵亡，唯一的一名幸存的侦察兵受了重伤，躲了几天，又被亲近美军的当地人捡到，收养了几天，最后被陆军的特种部队救了回去（看来美军也有“海军被包围，陆军去营救”的传统啊，例如格林纳达）。<br />
<br />
正所谓“渗透有风险，侦察须慎重”，这支四人侦察小组本来就不宜与大部队正面交战的，如果不是因为被牧羊人发现了，即使他们没有成功达到原定目标――找到名单上的塔利班头目，至少也能全身而退吧。结果最后既达不到目标，又导致损失一架直升机，11名海豹和8名160SOAR机组，战果是只干掉了几十名塔利班，这比起1993年在索马里明显是亏大发了。起码当年在索马里虽然损失了两架直升机和同样是19人，但至少抓到了要抓的人，又打死了大批艾迪德民兵和不明真相的围观群众。要是这队海豹没被牧羊人发现，该有多美好哇。<br />
<br />
但只此一个事件不能证实这个“牧民是特种部队的天敌”的观点，所以我们再来看看1991年的另一个例子――英国SAS的一支代号B20的侦察队在寻找飞毛腿时的故事。据说“红翼行动”将要拍电影了，而B20小队的事件，则已经拍了两部电影了。<br />
<br />
在“沙漠风暴”期间，萨达姆知道自己的力量不能抵御多国部队的进攻，为了把以色列拖进来，企图让多国部队中的阿拉伯国家发生内哄，伊拉克就不断用飞毛腿导弹袭击以色列。<br />
<br />
最初多国部队的指挥官没把飞毛腿看成是严重的战术威胁，所以在最早的空袭计划中几乎没关照它们，只在沙漠风暴的初始阶段摧毁了几个已知的飞毛腿阵地而矣。然而当飞毛腿的心理威胁几乎让以色列人出动空军时，美国人一方面竭力安抚以色列人，一方面让空袭部队暂时改变打击重心，集中对付飞毛腿导弹。但由于许多飞毛腿是采用流动发射器，又安置了大量的假目标，因此只靠卫星照片或航拍照片是很难及时发现目标的。<br />
<br />
为此，由于越战遭遇而一向看不起特种部队的施瓦茨科普夫不得不倚重美军和英军的特种部队渗透到伊拉克境内搜寻飞毛腿发射车。<br />
<br />
SAS派出A连和D连组成的四支机动小分队，使用轻型车辆在沙漠上到处游击。同时也派出B连对伊拉克境内从幼发拉底河谷到约旦边境的三条主要补给路线进行监视。B连的侦察分队由三个八人徒步小分队组成，分别向北部、中部和南部做道路侦察。由于A连和D连的奔袭行动取走了大部分的装备，所以B连装备不足，其中就缺少了M203的榴弹，地图也有问题。<br />
<br />
负责南部和中部道路监视的徒步小分队发现此地的地形不适合他们从事秘密活动，立即决定放弃计划返回基地。其中一个小分队通过直升机送回，另一个小分队则是徒步步行二百公里后回到沙特阿拉伯。剩下的负责北部道路监视的B20小分队决定冒险留下完成任务。<br />
<br />
B20小队在潜伏地点只呆了一晚，第二天早上就被一个伊拉克的放羊小孩给发现了。B20小队放过了这个小孩，虽然马上撤离，但也被大队伊军追上。SAS边打边撤，最终结果是最后两人死于低温症，一人阵亡，四人被俘，只有一个人只身步行二百公里逃到了叙利亚。<br />
<br />
此外还一个例子同样发生在沙漠风暴期间，那是在地面进攻开始以前，“绿色贝雷帽”派出了许多侦察队先行渗透到第18空降军计划进攻的区域，摸清这些地区的伊军实力和调动情况。当时，由第5特种大队A523小队调出的三个人所组成的008B特种侦察小组由黑鹰秘密送到加瓦姆阿姆哈姆扎尔附近的一个地方，然后步行四公里到另一个地方藏了起来。第二天早上，他们的藏身洞外来了一群贝都因人（这个阿拉伯语意为“荒原上的游牧民”），到了中午的时候，一个小女孩和她的父亲不知道是不是因为捉迷藏而闯进绿色贝雷帽的藏身之处，特种兵们没有开枪。这两个贝都因人也吓跑了出去，绿色贝雷帽也赶紧收拾东西离开。但其他的贝都因人以为他们是落难的飞行员，打算抓住他们去领奖金，于是三个绿色贝雷帽便与之交火。和前述的海豹小队和SAS不同的是，这三个绿色贝雷帽运气非常好，他们的通讯是正常的。所以他们立即呼叫空中支援，并请求空中撤离。虽然伊拉克人越聚越多，但在F-16的掩护下，这三人得以成功撤退。<br />
<br />
同一时间，还是第5特种大队的ODA525分队更倒霉。这是一支完整的A队，他们潜伏在一条伊拉克人的村庄附近，结果被几个玩耍的小孩闯进藏身之处（不知道是不是玩捉迷藏，只有玩这种到处钻洞的游戏才会老是有小P孩跑进特种兵的隐蔽地点），队员们问带队的军士长杀不杀，军士长说不杀。于是也是马上收拾东西就跑。然后大批伊拉克武装农民和军队追上，这十二个人里面有五个枪法很好的，在七百码外就和伊军的追兵交火，坚持一小时后空中支援赶到。伊军虽然同样也有增援，甚至有装甲车，幸好ODA525的人也是通讯良好，他们也接受过空中引导攻击的培训，所以在大批空中支援的帮助下，一直支撑到了MH60直升机赶过来把他们接走。（Youtube上关于ODA525的介绍 <a href="http://www.youtube.com/watch?v=wI6Xpn-Cp24" target="_blank">http://www.youtube.com/watch?v=wI6Xpn-Cp24</a>）<br />
<br />
上述这两个绿色贝雷帽的例子虽然都能全身而退，然而，正是由于小P孩和牧民的干扰，使得他们无法完成原定的任务。由此可见， 小P孩和牧民都是特种部队的天敌啊。<br />
<br />
