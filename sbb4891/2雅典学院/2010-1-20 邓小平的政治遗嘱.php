																											<div id="postmessage_442963" class="t_msgfont">////转来的，可能有人一看便说是假的，谁知道呢，反正就随便看看吧/////<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;今天找你们来，是我心中有些话想对你们说，人老了，清醒的时候越来越少了，我想趁我还清醒的时候，给你们交代一些事情。<br />
&nbsp; &nbsp; 　　你们三人，只有瑞林跟我快四十年了，从二十多岁的小伙子，也熬成了六十多岁的老头子了。而庆红和锦涛你们两位，今天算是第一次面谈。第一次面谈就给你们交心，是不是很冒失？是很冒失。我这辈子就是冒失过来的。早年不到二十岁，不懂法语、俄语，身无分文就冒失去闯法国、俄国。回国后，又冒失地到冯玉祥军队去工作；后来又冒失地去广西搞百色起义，到苏区又冒失地被打成反党分子；解放战争时冒失地挺进大别山。八大以后，毛点我当总书记，我却多年不向毛汇报工作。文革和文革以后，那就更冒失了。这些事情你们都知道，我就不罗嗦了。我把你们找来，要向你们交代一些我认为应该交代的话。<br />
&nbsp; &nbsp; 　　你们知道，年初我去了一趟南方，后来让郑必坚执笔弄出个“南方谈话要点”。很多人讲，这是邓老爷子的临终遗嘱，或者说最后的政治交代。这话不确切。我今后不会再说什么太多的话了，但真正的政治遗嘱是不会像这样弄得满城风雨的，真正核心的政治交代怎么能大张旗鼓地宣扬？！今天，我倒想小范围的真正讲一下我的政治遗嘱，或者说是真正的政治交代。<br />
&nbsp; &nbsp; 　　首先，我对我们国家的政体现状并不满意。我是这个政体的创建者之一，这十几年也算是这个政体的守护者、责任者，但我也是这个政体的受害者。每当我看到朴方残废的身体，我就在想，我们政体的名字叫中华人民共和国，但共和国最本质、最核心的东西是什么呢？应该是民主与法制，而我们所缺的恰恰是民主和法制！为改变现状，这些年我做了一些工作，这个问题并未解决，十几年后，你们当政时也未必能解决。其实解决的办法是存在的，这就是向美国的宪法学习。美国成为超一流强国，靠的就是这个东西。中国要成为一流国家也得靠这个东西。向美国学习，应该理直气壮，你比别人差嘛，就应该承认自己的不足。当然，这里边有很多技巧，不要急。但你们有责任去努力、去学习、去实践，这是历史的责任。经过几代人的努力，把中国真正建成权力来源于人民，法制公平的宪政国家。这也是孙中山的梦想。只有这样，才能说长治久安。<br />
&nbsp; &nbsp; 　　第二，台湾问题。香港问题解决之后，中国最大的统一问题就是台湾。台湾问题的关键所在是现在政体上差距太大。解决这个问题我是看不到了，你们那一代也未必能解决。但我想有三点你们要把握好：一是不到万不得已绝不动武，中国人不打中国人。二是大陆的经济要奋起直追，你一直穷下去就永无希望。三是在政体上大概一国两制还不够，一种可能的方式是联邦制宪政之路。中国经济强大了，政治上又有了民主和法制的共和体，台湾问题才有可能迎刃而解。<br />
&nbsp; &nbsp; 　　第三，发展问题。上面两个问题的基础还是经济建设和社会发展。而这个问题有核心是发动老百姓去干，而不能只是政府去干。要千方百计让全国老百姓的脑袋来代替总书记、总理的脑袋，我们再聪明也聪明不过人民，我们的政府管得太多了，要尽可能少管。经济上，老百姓和市场都比我们的计划聪明。我想，只要坚持开放改革，坚持以经济建设为中心，放手让老百姓去干，也就是坚持不断地发展经济民主，每年增长超过7%是有希望的。坚持下去，持之以恒，等你们交班的时候，中国或许就成了小康国家了。<br />
&nbsp; &nbsp; 　　第四，中美关系。中国对外关系中最重要的是中美关系，回顾一百年来，对中国欺负最少的大国就是美国了。退回庚子赔款，让中国人到美国留学不说，八年抗战，美国的援助比苏联的援助多得多！抗美援朝与美国打仗，是金日成和斯大林加给我们的。美国是第一强国，中国的发展和统一都绕不开美国，世界和平与发展也离不开美国。现在为了稳定和发展，我们只能韬光养晦，绝不冒头。没办法，我能力不够，手段也有限嘛。到了你们这一代，办法可能会多一些。我们要学习美国宪法，美国人会不开心吗？为了民富国强，我们党让人民当家作主和富强的理想不变，但名字是否可以考虑改为人民党、社会党之类呢？我想名字一改，中美关系马上会改善。总之，到了你们那一代，手段会多些、办法也会多些。你们也要开明些，灵活些，要有所作为，不要像我们这一代这么僵化和死板。只要为了国家和人民利益，实事求是地去做，就经得起历史的考验。<br />
&nbsp; &nbsp; 　　第五，“六X”问题。“六X”是改革开放过程中的必然现象，社会成本很高。这个问题，今后会有人来翻旧帐，说你动用了军队，也死人了，责任是躲不开的。但也还有历史责任，则在于国家是前进了还是后退了？国家是混乱破败了还是稳定发展了？真正对历史负责任的人，不怕这种责任。尤其要做领袖，更得要有担当。到了你们那一代，也不知会出什么样的事情，或许是“六X”，或许是“七四”，但你们一定要有历史责任感，实事求是，一切从实际出发，只要对中国进步发展有利，该怎么干就下决心怎么干。回答“六X”这类问题，根本的方法不是去争论，而是实实在在地把国家搞好，让人民生活一天天好起来。有人告诉我，党内人材一代不如一代，我看得以什么标准衡量，论文采飞扬，我不如毛泽东，论意志坚定，你们可能比不了我。但论科学理性，论勤学努力，论民主开明，可能是你们的长处。总之，不要怕事，不要怕祸。要敢闯、敢干、敢负责任。当然，也不要一朝权在手就惹事生非。要不惹事、不生事、干实事、敢负责，有了这种态度，历史也会对“六X”有个理性的说法。<br />
&nbsp; &nbsp; 　　第六，制度建设。除了政体要在宪法制度上下大气力外，还有党内、政府内的政治制度搞一些持之以恒的建设。像今天我们只在小圈子里选江泽民，小圈子里选你们，这是历史条件，没有办法。但这种作法绝不能长期不变。最终的领导人还是靠人民来选，不能靠小圈子和枪杆子。最好是先从基层的民主抓起。今后我们再也不是枪杆子里面出政权。古语说是得民心者得天下，我看得靠实事求是的本领，靠真理和民心民意来维持的完善政权。你们要有这个观念，今后主要是靠老百姓的税收来养政权，你要老百姓养你，你就得代表民意和服务民意。此事从上到下搞，风险太大，但必须试验，不搞的风险更大。合理的办法是从下而上慢慢演进，先把基层工作做通，农村包围城市，这样风险较小，就像八十年代农村改革那样，先从大包干抓起，而后是乡镇企业，再而后是城市改革和国有企业改革。制度改革也可以摸着石头过河。不要急，但也绝不能不去开拓进取。<br />
&nbsp; &nbsp; 　　最后，关于你们和我家一些个人问题。朴方服务于残疾人事业，三个丫头都有自己的事干。我担心的是质方，他一介书生，不善与人交往，不能让他从政和理论研究，他要经商就由他去罢。但你们要帮我监管他，不能让他搞大，做一个普通人最好。瑞林到军队工作，努力做好泽民同志的部下。另外，你们两位，能够走到今天，也是你们的本领。苏东问题爆发后，我向政治局说过，要沉着应对、稳住阵脚、冷静观察、韬光养晦、绝不冒头。这话也适用于你们。在泽民同志主政时，你们当好助手。今后你们主政时，这20个字仍是做大事应该注意的。只是在20个字后面，再送你们四个字：有所作为。</div>

							
							
							
							
