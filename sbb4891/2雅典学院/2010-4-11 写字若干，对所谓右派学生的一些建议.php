													<h2>写字若干，对所谓右派学生的一些建议</h2>
						
						
																											<div id="postmessage_558689" class="t_msgfont">转载<br />
<br />
昨天在与几位老师吃饭时谈到时下的社会问题，教我历史文献的老师说，综合历代情况，当下的中国与明末最为相像。<br />
他后来还说，尽管后来崇祯上台后励精图治，而且还有人说他能中兴明朝。然而后来的事实告诉我们明季垂暮，此后并没有再坚持太长时间，更别说中兴了。<br />
往事已矣。更重要的是如何认清当前中国的社会现实。<br />
我上人人并不久，加了一些经常讨论时事的好友。在看了不少的相关文章后，我发现了一些问题：<br />
一，&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;尽管很多被贴上“右派”或“自由派”标签的朋友都抱有极大的热情，藏具深入的见识，然而说白了，不过是每天定时上网与五毛论战而已，在与装备强大宣传机器的D政的最基层，甚至编外的小喽们纠缠不清，影响力实在有限。<br />
二，&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;人人尚好，若翻墙上twitter，一个个看下来，当前的国内局势简直是大厦将倾，一片山雨欲来之势。然而事实是否如此？我的亲戚中有不少的人还数不清目前中国到底有多少直辖市，甚至还有人不知道河南是个省，并且这些人都是年轻人，他们天天做的事就是玩玩劲舞，打打魔兽，麻将扑克，不亦乐乎。你能指望这些人对将来有什么看法吗。韩寒说“他们的需求是不能想象的低，他们的忍耐力是不能想象的高”。我例举这些不是为了批判什么愚昧无知，只是为了说明所谓的有识之士根本上与普罗大众是脱节的，在社会底层没有话语权。<br />
三，&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;目下的社会事件层出不穷，很多朋友对政府群起攻之，晒黑揭弊。这是好现象，起码比沉默的大多数要光荣的多。然而其中的问题是：有些只是流于就事论事的层面；大多数的支持充其量算作声援；最重要的，缺乏组织性。<br />
 <br />
我们现在处在某种“前夜”的阶段，前期多年的演变已经完成，即将发生质变。而且从目前的情况看，这种质变多半是国人不愿意看到的。可是这种质变的主导权不在任何人任何团体手里，过去问题的积累已经完成，积弊难返。<br />
所以如果我们不想继续这几千年的历史循环的话，必须全社会“合作”。我作为一个学生，谨发表我自己的观点，希望可以给所谓“右派学生”这一群体做一些建议：<br />
一，&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;学生群体向来是对国家前途社会未来极具热情和责任心的，无论是古代的太学生还是以前的大学生，当然，不怎么包括现在的大学生。所谓“右派学生”现在应该是少数派了，拜D政的强大，和多年的扩招，具体论证不叙。右派学生虽然弱势，然而更应该担当启蒙和更新之责。我盼望那天时机成熟能有相应的学生组织出现，非法结社也不在话下，组织是集体发声的前提，不能这样无以争取话语权，无以提升影响力。这是建议一。<br />
二，&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;当前虽然在某些领域，例如人人上，学生对社会不公的讨伐略有声势。然而缺乏鲜明的有前瞻性的思想。未来的中国是肯定会发生某些变化的，但至于身处其中的每个群体的任务的问题却很少有人会注意到。我国未来的道路必须立足改良，任何一个有影响力的群体的冲动都会造成非常恶劣难以挽回的结果，而且作为被统治的一方，现在的中国没有任何挑战政权合法性的可能，国家已经将触手延伸至社会的方方面面角角落落，社会已经窒息。所以学生作为具有知识的群体，应该清醒地认识到这一点。当前的局势，只能用现代的方法予以一一化解。这里所指的“现代的方法”是宪法规定的任何合法手段。这是建议二。<br />
三，&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;二十多年前的“柳丝”失败，有一个关键的原因是，学生并没有一个明确的促进改良国家的计划，或者，没有一个可行的计划，而且并不明确身处其中的自己的任务。我认为现在的危局虽然积弊已深，然而还是有化解的方法的。第一大步是解放被国家窒息的社会。任何改革都要立足实际，“民*主”的口号不是空喊的，如果将来中国还有改革可以推动的话，那基层民主的构建将是事关成败的关键，不要动不动就去质疑D的执政…合法性，不要眼高手低，要动摇国家对社会的绝对控制，必须从最基本的入手，基层民主的构建和扩大化将会是一切改良的基础。而对于学生来说，最重要的是校园民主的建设，稀释D政的势力。当然这很有难度，需要作出努力和牺牲，还必须有合适的时机。<br />
 <br />
我国最大的问题就是社会资源分配的问题，而要解决这个问题则还要请出“德先生”来。而民*主的建立与施用，其又必须以普罗大众的启蒙为前提，启蒙这个任务在我国还远未完成，甚至某些时间内还有倒退的迹象。</div>

							
							
							
							
