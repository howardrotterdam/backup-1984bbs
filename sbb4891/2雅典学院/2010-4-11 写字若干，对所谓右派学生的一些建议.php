<br />
你说中国经济到了“十分危险的边缘”，我不敢苟同，尽管中国在政府对经济的干预和越来越强大的既得利益集团方面有缺陷，但宏观上来看，中国经济长期依旧是利好的。<br />
<br />
1.中国有很多很多向往富裕，能吃苦，勤奋工作的人们；中国有无穷多充满创造力的企业家，在各个领域寻找利润的空间。<br />
2.中国的高储蓄率（也许是过高的储蓄率），虽然也许超过了最优的水平，但为经济增长提供了源源不断的资金需求；<br />
3.中国没有有效的工会组织<br />
4.中国银行效率虽然不高，但相比银改之前已经大大提升，非复吴下阿蒙。<br />
<br />
所以，对我而言，谈论中国经济的崩溃是一件十分难以想象的事情。<br />
<br />
当前流行的为什么中国有泡沫/要崩溃的理由无非几个：<br />
<br />
1.去年信贷狂增，接下来就是大量坏债和通胀；<br />
2.中国的房地产存在泡沫，特别是去年有大量的银行资金流入，导致房地产价格过高，一旦泡沫破裂，直接拖垮中国经济体系；<br />
3.地方政府通过融资平台大量借贷，投了过量的基础设施，迟早会是一个定时炸弹，会导致巨额的财政负担或者银行坏债。<br />
<br />
前面提到的第一点和第三点，总的说来是担心中国去年发生的巨额投资的，在我看来，不管中国的投资有多大，中国宏观上仍然是一个资本相当稀缺的国家。中国的投资，在当前的阶段，很难会过多。中国独特的金融市场加上政府主导，使得中国的投资结构往往不好，有些投资在短期会使过剩的，比如说修一条几年不跑车的高速路，或者建一个几年都住不满的新城。这些没话说都没有实现资源的最佳配置，但这些投资大多数只能算超前，回报会推迟一些而已，认为这些投资中的一大块会是永久性的浪费，我很难相信。至于通货膨胀，虽然会造成很不好的分配影响和社会后果，但那些说高通胀的人也就是在说5-6的通胀率，这怎么说也不能算是致命性的通胀。<br />
<br />
