													<h2>“75事件”――一场失败的政治阴谋</h2>
						
						
																											<div id="postmessage_154712" class="t_msgfont">从事件一开始，脑袋中的第一个想法便是阴谋，这个想法随着事件的发展，一点点的开始清晰，开始确定，直到现在非常想写出来，观点对与错还需前辈们指点。<br />
<br />
首先来看一下这场事件我为什么将它定为政治阴谋的几个关键：<br />
<br />
一、<br />
有网友说，早在事件发生之前，当地QQ群和BBS就已经公开要在7月5日进行游行。也就是说，在人们都知道这件事的情况下，官方必然也知道。既然官方知道了，游行为什么还会发生？想一下，如果这件事情发生在内地的任一城市，在事先知道会有群众游行的情况下，如果党国允许这场已知的游行发生，那可真就是笑话了。但在乌鲁木齐却发生了，这本来就是一笑话了。<br />
二、<br />
不排除游行队伍里真的有“分裂势力”，但也不排除游行队伍里会有“党的人”，在游行发生后，何以成为暴乱的？不是分裂势力的策划便是有线人在里作祟，煽动群众这一小小的伎俩党国的政客们还是运用的相当到位的。<br />
三、<br />
动乱发生后，为何警力没有迅速到场。从开始一直到烧了那么多车辆店铺，死了那么多人，警方应对这种事情的效率会有这么低？我不信，你信吗？之所以开始的时候没有动用警力去“干扰”他们暴动，我想应该是党国希望这件事情被无限扩大。没有死人，就没有正当的理由去镇压，就不能抓一些人。<br />
四、<br />
从那些曾经的图片，和制作的图片，我们不难看出党国确让想让民众知道那里发生了多么惨的事情。<br />
五、<br />
关于国外记者，谁见过中国的这种事情会有国外记者出现？这跟本就不是什么党国在这点上的进步，而是利用这些进行他们的这场阴谋。<br />
<br />
其次要来了解这场阴谋的目的是什么：<br />
一、<br />
在新疆范围内彻底清查“反动势力”和“分裂组织”。这个是肯定的，新疆确实也有这些，而且每年都在对当地的百姓制造威胁，党国需要干掉他们，为了“领土的统一和人民群众财产的安全”需要干掉他们。<br />
二、<br />
杀鸡给猴看。今年出现了太多群众示威游行，党国需要借助一场清剿来杀鸡给猴看，来告诉百姓，没什么事别跟着凑热闹瞎咋呼，稳定国内局势，对不法份子严惩，这无非就是杀鸡给猴看。<br />
三、<br />
给国外的“敌对势力”上点眼药。说白了，国内的这些分裂组织和反动势力都是有国家支持的，这些国家包括欧美的大国们，干掉一个“世维会”就是给一些国家上眼药，让这些国家在民众的心目中丧失地位，达到某些不想让百姓知道的目的。<br />
其他还有什么目的我没琢磨出来，需要持同一观点的伙计来琢磨了。<br />
<br />
为何称之为一场失败的政治阴谋：<br />
一、<br />
死亡人数。我相信党国也没有想到这场事件会导致这么严重的后果，会有这么多人丧生，这么多店铺车辆被摧毁。群众有死亡本应该就是算在这场阴谋之内的，但是党国从第一次公布到第二次公布这之间，这个转变太突然了，以至于更多的人认为是镇压了，这就是失败。<br />
二、<br />
汉族反抗。这应该是他们没有想到的。估计他们怎么也不会捉摸到汉族人竟然会进行报复。汉族的反抗报复使得这事件进一步扩大，同时也破坏了只逮捕维族恐怖份子这一路线，以至于不明真相的又一些无辜群众也被卷进这个阴谋，使得党国不能按照原定计划进行，损失了一部分用来宣传的时间。<br />
三、<br />
西方记者。西方记者的介入本来应该是这一阴谋的帮凶，却不料一些西方记者们学会了CCAV的一套，开始不说真话，甚至诽谤。这就导致了党国借助西方媒体宣传扩大这场暴力的计划在一些西方媒体上变成了自取其辱。同时因为汉族的反抗，加上西方记者的添油加醋，最后刻画成了一场民族之间的矛盾。要知道，种族矛盾是这个世界上最有料的话题，党国让这这个话题弄了个措手不及。其实如果要真是什么种族问题，纳粹一下比什么都利索，但我想党国的本意没这个，甚至我觉得拿种族问题说事的也全都是傻逼。<br />
四、<br />
唉，本国的媒体啊，你们太让百姓爱戴了，你们简直就是在变相揭发党国的阴谋，你们所唱的每一出戏都是阴谋的影射啊。<br />
<br />
<br />
结局呢：<br />
当然，该逮该收拾的肯定还会收拾，阴谋其实还是圆满的成功，不过就是要多一些小插曲，比如说造个假，唱个颂歌什么的。从大局考虑，这场阴谋还是走的正，拿部分百姓的生命财产换取一个地方的安宁。在保证地方安宁和祖国领土完整这一点上给予党国充分的肯定，但是在玩弄百姓生命这一点上就草泥马了，使劲使劲草泥马。</div>

							
							
							
							
