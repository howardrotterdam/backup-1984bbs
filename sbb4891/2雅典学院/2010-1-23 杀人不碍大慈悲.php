													<h2>杀人不碍大慈悲</h2>
						
						
																											<div id="postmessage_448224" class="t_msgfont">杀人不碍大慈悲！<br />
<br />
在儒家，良知是天地之间最高的标准，最高的原则，最高的真理，最大的“法印”，超越一切外在戒律和世俗规范。良知至善至仁至公至义天下至诚，凡与之抵触的社会规范必是不道德的，凡与之悖离的法律必非良法。<br />
<br />
对于穷凶极恶之徒、十恶不赦之罪、杀父害民之贼，是不能听之任之的，该骂就骂，该打就打，该杀就杀。故我在《仁者无碍，得大自在》中写道：“只要合乎道义，开骂是义愤，行刑是义刑，开战是义战，杀人是义杀、天杀（替天而杀）”这都属于致良知的“工作”。<br />
<br />
社会需要公正，人心需要正义，天道需要“落实”，罪恶需要清算。如果一味地屎来头挡、刀来脖挡，一味地以善对恶、以德报怨，就是对自己、对他人、对社会的不负责任，生命之尊严就无从体现，人类之公道就无从建立。<br />
<br />
有人问了：你不是说“人是可以被唤醒的。睡得最沉，哪怕睡成石头或者冰也可以被唤醒”的吗？怎么又主张骂、打、杀呢？其实，骂、打、杀，都是一种呼唤，方式不同，目的不异。一方面，人心皆本之于“天”，可以相通；另一方面，太多人的本心不明，被自身不良欲望和外在不良环境所染污所障蔽，很难相通，故唤醒的方式不拘一格。<br />
<br />
人之本心慈悲不可思议、智慧不可思议，但人之习心恶毒愚顽也不可思议。对那些善性已绝（佛教称为“一阐提”）而恶行不断的人，为了维护公道、替天行道，是可以也应该予以公平“报复”和惩处的。<br />
<br />
在正常的社会里有法可依，一切当然依法解决，特殊情况无法可依或法律成了帮凶时，则不妨采取极端手段从权处理。循良知而行，就可以获得随心所欲皆不逾矩的绝对的意志自由。在无法可依的据乱世，仁者可骂人、打人乃至杀人。<br />
<br />
有人认为儒家在这方面太讲“权道”，不够尊重生命，不象佛教那样严戒杀生！这种质疑是对儒佛两家皆一知半解所致。确实，杀生是佛教根本大戒，但这大戒不是绝对不能开的。佛法强调契理契机。杀戒也同样有个是否契机的问题。如果面对屠刀不去反抗，见人行凶不去打击，一味坚持“不杀生”，只能导致恶人横行恶果累累。<br />
<br />
佛教认为，只要满怀慈悲不起_恨，威即是德，大威即是大德，制恶伏恶即是导善行善，霹雳手段即是菩萨心肠的表现。菩萨或为了调伏顽劣的众生，或为了遏止恶人的恶行，有时应机示现金刚怒目之相。藏传佛教中，大威德布畏金刚是文殊菩萨的愤怒化身，大威有伏恶之势，大德谓护善之功，故名大威德布畏金刚。可见佛法并非绝对禁杀。经载: 波斯慝王有一天禀告释尊：佛陀啊！我国中有大盗贼，我领兵讨伐, 把贼人杀死了, 犯了杀业。释尊回答：善哉善哉，你并没有做错。<br />
<br />
又有一个佛教故事讲：一个菩萨发现一个商队中混进了一个强盗，这个强盗准备寻机把商人们杀害后抢走财物。菩萨寻思：我杀了强盗，就犯了杀戒，就要堕入地狱，而不杀强盗，就会导到更多的生命被杀害。最终这位菩萨以我下地狱也要拯救众生的精神，把这个强盗杀死了。<br />
<br />
对佛教徒而言，杀恶不仅为了保护良善，同时也是对作恶者的怜愍，断绝其造下更大恶业的能力。平时严守杀戒，是慈悲，特殊情况下勇于开杀戒，则是大慈悲。必要时勇于杀人者不是凶恶到极点，就是慈悲到极点。印顺法师认为：<br />
<br />
从大智的契合真理，大悲的随顺世间来说，戒律决非消极的“不”、“不”可以了事；必需慈悲方便的能杀，能盗，能淫，能妄，才能完满的实现。如有人残害人类──有情，有情因此遭受难堪的苦迫。如不杀这恶人，有情会遭受更大的惨运；恶人将造成更大的罪恶，未来会受更大的痛苦。那末宁可杀这恶人，宁可自己堕地狱，不能让他作恶而自害害他。这样，应以慈悲心杀这恶人，这不是杀少数救多数，是普救一切，特别是对于作恶者的怜愍。因为怜愍他，所以要杀他。但愿他不作恶业，不堕地狱，即使自己因此落地狱，也毫不犹豫。对于杀害这个人，是道德的，是更高的德行，是自愿牺牲的无限慈悲。（印顺法师《妙云集》）<br />
<br />
密教有诛法，所诛对象，除自身的烦恼及我执之外，对于恶人亦可驱遣鬼神予以诛杀。据陈健民居士介绍，“至于无上密部不共之诛法，世人罕知其意含，故余择要条举于下：一、发心必为维护正法，不夹私怨。 二、对像必有破坏正法之行实，且为弘扬正法之重大障碍。 三、虽是诛法，减彼寿命，而有超渡之功能，能迁彼识至佛之净土。”（《曲肱斋全集》陈健民著，徐芹庭编）驱遣鬼神杀人可不可信是另一回事，但说明了密教不讳杀。<br />
<br />
可见，真正佛徒、圣者是兼具“霹雳手段菩萨心肠”的。所谓“除恶始为真佛子，杀人不碍大慈悲。”<br />
<br />
当然，杀人要有极高的道德资格。不少人喜欢以“霹雳手段菩萨心肠”自许或誉人，大多属不解真实意的口头禅。霹雳手段不能伤害良善只能制恶利生。霹雳手段不能随便用、也不是谁都能用的，必须以菩萨心肠为根基和前提。菩萨心是大慈大悲之心，“大慈与一切众生乐,大悲拔一切众生苦”，没有这种精神，未达这般境界，是不配称菩萨心肠也没有资格用霹雳手段的。枭诗曰：<br />
<br />
自性菩提自在身，也能欢喜也能嗔。是真佛子能除恶，具大慈悲勇杀人！<br />
