　　刘家远回乡后，分了土地还分了房子，老老实实地干活种田，渐渐生活也有了些起色，娶妻生子，小日子也还过得有些兴旺。乡亲们都说他娶了个能干泼辣的女人，刘家远家旺人兴有了指望。<br />
　　无论是互助组，还是合作化，运动一来，干部一号召，大会、小会一开，一说不加入就是思想落后、不想走社会主义道路，还拿他是复员军人说事，要他“起带头作用”。刘家远胆小怕事，吓得战战兢兢，回家就要拿土地证，牵牲口。他老婆死活不同意，两口子每每都要大吵大闹一番。但刘家远总是带头入组、入社。合作社生活一年不如一年，两口子意见也越来越大。<br />
　　人民公社化运动中，澧县的“共产风”在全地区都是有名的厉害。而庐山会议后，全县再一次刮起了十二级“共产台风”。<br />
　　1961年4月整风整社运动中，县委书记张琢，在县委扩大会议上的检查报告中说：“这股‘一平二调’的‘共产风’，从五八年公社化后起，到这次整风整社运动开始止，才平息下来。据初步统计，公社化以来，全县各级刮的‘共产风’，折合金额达到3812万元，相当于三年农副业总产值（15732万元）的23%；其中光是刮的社员个人的各项物资折合金额达到1246万元，相当于五九、六0年两年分配给社员部分的30%。其中包括集体土地29563 亩，社员自留地24307亩，房屋123960间，各种农具3823837件，现金2699000元等，每户平均90.4元,人平24.77元。<br />
　　特别是拆房屋，挖祖坟，叫社员餐风露宿，几代同房，公公和儿媳妇住一起，三五家人家挤一间房，最使社员伤心和恼火。社员形容那种情况是：“公媳同房，帐子做墙，说话为难，洗澡过关。”<br />
　　如东公社的“共产风”越来越大，“一平二调”刮得树倒屋毁，家破人亡。土地、自留地、房屋、耕牛、牲猪、羊、鸡、鸭、鹅、锅、坛子、桶、刀、铲、火钳、钵、碗、菜盆、鼎锅、撑架、箱子、床、柜、桌椅板凳、犁、锄头、锹、粪桶、扁担、箩筐……<br />
　　全社“一平二调” 社员各项物资折合金额达到172076元，每户平均106.8元。刘家远家中被搜得一干二净，就连桌、椅、板凳、刀、铲、钵、碗也没留下半点。<br />
　　如东公社各级干部的强迫命令、违法乱纪也非常严重。据张琢在一次整风运动的县委扩大会议上说：如东公社的干部对人民群众采取了法西斯统治，随便打人、骂人、捆人、吊人、扣粮、斗争、“辩论”、罚吹西北风、吃草、吃泥巴、喝尿、吃牛屎、灌大粪、儿打娘、敲牙齿、针缝口等。公社成立劳改队，有的大队还成立了改造队。开辩论会的时候，先把积极分子教好，让他们起带头作用，然后干部吹哨子为号，一起涌上去打，哨子控制节奏，吹得快，打得要快，吹慢就慢打，吹得声音大就打重，吹得轻，就打轻点等等。<br />
　　1958年9月，干部带头从刘家远家中，抄出他老婆私藏在床下的家中全部现金25元钱不交公。<br />
　　常言说得好，人善被人欺，马善被人骑。别人挨打，家族中有人的，或者本人刚强的，下手者还有些手下留情。而刘家远人老实，平时就被人欺负惯了，这回两口子更遭了大罪。那年月，对老实人，不打白不打，打了也白打。他被人打得口吐白沫，他老婆被打得一头栽倒在地，就连两岁多的儿子也被人一脚踢得半天没哭出声。别人老婆挨打，做丈夫的无论如何也会跳起来拼命；他老婆挨打，刘家远缩在墙角不敢吱声。干部们走后，他老婆又哭又闹，口口声声骂他是个废物，没得本事，连老婆都护不住。<br />
　　家里分文无有，两口子连看这场伤病的钱也没有。还是邻居好心，去扯了些治伤的草药来，两口子过了好些天才慢慢恢复。<br />
　　1959年9月，如东公社各大队搞起“反偷风” 运动。大队和生产队一级级开大会，要社员老实坦白交待从1959年春荒以偷盗集体庄稼的罪行，说是“党的政策，坦白从宽，抗拒从严。自己积极交待、交待退回的，既往不咎。抗拒不交的，罪加三等。”<br />
　　审到刘家远时，干部硬逼他老实交待。他在惊恐之下，承认说偷过一次苞谷。其实当时苞谷在地里还刚刚结穗，显然是强逼屈招。结果干部们硬说他偷了一次有二次，有二次就有三次。前前后后给他算成偷盗集体粮食20斤，硬是逼他交出来，并带民兵到他家抄家。干部们把他老婆在收割中稻时从收割后的田里捡的5斤多谷子全都抄走，公共食堂又罚他一家三口人停伙两天。<br />
　　是年冬，因家中断粮断炊，他老婆实在饿得受不住了，于是丢下刘家远和儿子，跟着一个男人跑到外乡了。<br />
　　1960年春节前后，家里粒米不剩，刘家远父子常常连清水煮野菜也吃不上。别人家为了救命，也顾不得人民公社的种种禁令，不少人偷偷私自跑到湖区捕鱼、挖藕，搞点吃的。刘家远胆小怕事，坐在家里干挨饿。大人都受不住，孩子饿得一天到晚哭哭啼啼，围着他要妈妈、要吃的。刘家远心烦意乱但又一筹莫展，常常坐在屋里独自发呆，谁也不知道他在想什么。<br />
　　大年三十晚上，邻居见他家实在是太可怜了，便给他端过来一小碗野菜粥，父子俩这次总算吃了顿年夜饭。<br />
　　这时，公共食堂一天开一顿伙，大人一人一碗见不到米星子的野菜粥，小孩子才给一勺子。刘家远的“饭”，常常被孩子吃了还不够，整天围着他哭闹喊饿。这时刘家远自己也饿出了浮肿病。<br />
　　不久，公共食堂停伙断炊。刘家远父子俩已经几天没吃饭了。这天，孩子哭了一整天，到了晚上家里声息皆无。据刘家远后来自己交代，这些天里，他自己一听到孩子的哭声就烦躁不安，还经常出现幻觉，就连做梦也是梦见白米饭、红烧肉和红烧鲤鱼，还常常把走在路上的人，看成是在动的猪，想吃肉都快要想疯了。<br />
　　当天夜里，他看到躺在铺上的儿子饿得就快要断气了，恶念顿生。心想，与其让儿子受罪，还不如把他杀吃了，吃个饱肚子自己再死也好。于是他找来一把破菜刀，几刀就把他儿子杀死了，并肢解成几大块，用水洗干净。又偷偷跑到地里，偷了一些生产队的胡萝卜，洗干净后，放到破鼎锅里，和儿子的尸体一起煮。<br />
　　那天晚上，刘家远说他几年来第一次吃了餐过瘾的肉！<br />
　　第二天，邻居没见他家小孩子哭闹，“连人影子也没照过面，感到有些奇怪，就到他家里看看。”这一看可把人吓坏了。只见几块砖头搭起的灶上放个破鼎锅，鼎锅里还煮着小孩的头、胳膊和腿脚！于是赶紧跑到公社去报案。<br />
　　刘家远被捕后，一五一十地交待了他的罪行。说就是不想让他儿子再受罪了。还说他的想法，是吃完了儿子自己也死了算了。<br />
　　刘家远被枪毙前一天晚上，监狱破天荒给他端来了一碗大米饭。据同号的犯人说，刘家远竟把米饭放在地上，先祭了一番死去的儿子，嘴巴里不知道念念叨叨些什么，然后几大口就把一碗饭吃了个精光。<br />
　　刘家远案在澧县影响很大。大概直到1964年初，随着人们的兴奋点转移到“四清”运动对干部“四不清”问题上去，刘家远案才逐渐淡化出人们的视野。但是，对小孩子而言，刘家远案是永远的恐惧！<br />
<br />
（余习广）</div>

							
							
							
							
