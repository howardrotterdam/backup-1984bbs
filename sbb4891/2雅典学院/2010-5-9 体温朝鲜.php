													<h2>魔鬼教官在朝鲜</h2>
						
						
																											<div id="postmessage_609368" class="t_msgfont">体温朝鲜<br />
&nbsp;&nbsp;黄章晋<br />
<br />
&nbsp; &nbsp; 　每到节假日临近，我总会想，这次得要去看看朝鲜了，万一再看不着这个国家该多后悔。<br />
　　几年前，一位日本同行邀我见识北京一家朝鲜餐厅，其间，他不断表白自己是个强烈反对Koizumi（小泉）的“好日本人”，衷心希望朝日友好，宴席散场，他又很在行地向及时表示对朝鲜歌舞的热爱，于是，一班人马盛装为我们两三个人热情表演。我很尊敬这位同行的职业水准，但他夸张地赞美朝鲜，为他们鼓掌喝彩作沉醉状，让我满心觉得，他其实是在残忍地欣赏他眼中的野蛮和愚昧表演，――这个世界的怪胎国家惟有朝鲜，谁知道它明天是否还存在，不装傻充愣鼓励他们没完没了地表演，哪里还有机会再看到这样的猴戏。也许是我太过敏感，我无法与他一同津津有味：中国与朝鲜，实在是五十步与一百步之别，无论是文明人为迎合野蛮人的自尊而故作欣赏之态，还是文明人在觉醒了的野蛮人诉说自己的苦难时礼貌的悲悯，我都会有不自在的联想。<br />
　　朝鲜，总能带给我这种复杂的情感。<br />
　　当21世纪的太阳金正日乘坐专列跨过鸭绿江大桥时，我正在丹东一家酒店早早起来，等着组团一起过境。虽然google earth上有大批狂热的“邪恶轴心研究者”，把朝鲜看得比中国、伊朗还重，朝鲜三千里江山的每一寸都被他们细细标注过，但下载插件在空中研读，还是无法替代被限制了的实地亲眼一睹，是好奇，还是小心翼翼地证实一下自己的判断，甚至是怀旧？一言难尽。<br />
　　看多了渲染朝鲜贫困的中国人，若想到朝鲜满足一下贫寒乍富的优越感，多少会有一定的失望。朝鲜不是当年完全封闭的中国，至少对外部世界的商品是开放的，――除了韩国制造，而且朝鲜远不像当年的中国对国民装束有无数愚蠢的禁则，所以，能被你看到的朝鲜人，穿着打扮并不明显比中国人落伍。在新义州的领袖事迹纪念馆前的广场，我们碰到一对新人，他们的装扮丝毫不逊于中国新人，在公园再次碰到他们时，他们的家人各个手持相机、DV，由于这对新郎会流利汉语的新人与我们总是如此巧合，我甚至起了疑心：如果下次有朋友们再碰到他们结婚，请代问一声好。<br />
　　中国各种新奇的商品，朝鲜也有，事实上因为开工不足，朝鲜很大一部分商品就是中国制造的，只是多少以及什么人拥有的问题。朝鲜的对外半开放，甚至使其制作的动画片，也比中国的更得迪斯尼精髓，远不似“喜洋洋与灰太狼”那么弱智。<br />
　　赞美朝鲜是个平等社会的人，恐怕也会失望，如果心细的话。朝鲜贫富分化可能远比中国严重。朝鲜大街上空空荡荡，只有两种车，一种是破破烂烂的车，甚至平壤都有木炭车，一种是媲美山西煤老板的高档车。与中国游客保持密切接触的朝鲜人，穿得不差，长得不差，除了没有胖子，与中国人差别不大，但透过玻璃，你总有机会看到那些接触不到的普通人。尤其是在远离宏伟建筑的郊区和农村，所有人都长得与社会主义国家宣传画上红光满面身材粗壮喜气洋洋的公民形象完全相反。<br />
　　60年不同的社会制度试验，朝鲜南北变成了两个种族，韩国人平均身高与南欧相近，而朝鲜人则可算世界身材最袖珍的民族。当然，社会主义制度也会在穷国本土制造出身材明显区别的两个种族。刚到北京的头两年，我惊讶地发现，比我年长一辈的北京大院子弟多有着惊人的身高，这种差别在朝鲜更明显。这次正逢着朝鲜征兵时节，从外地汇集来的新兵，看上去都像发育不全的小学生。<br />
　　而新义州本部幼儿园的孩子们，享受的师资和教育条件中国普通幼儿园根本无法相比的，他们是朝鲜未来的主人翁。每个教室窗口望进去，老师谆谆诱导孩子的动人场景，拍下来都是一幅阳光希望温馨健康向上的宣传画。孩子们的表演，更非中国幼儿园可比，这里的每一个儿童都有着比林妙可还要林妙可的懂事乖巧天真烂漫可爱。有几出极为精彩成功的节目，我不知道该不该与周围的同胞一样热烈鼓掌，我知道他们是真的喜欢那些孩子的表演，但我无端地想起与那位日本同行在朝鲜餐厅的情形，我只觉得我鼓掌不鼓掌都是一种残忍。万分激动地歌唱金正日将军，与歌唱我们是祖国幸福的花朵，有很大区别么？<br />
　　毫无疑问，这个曾跨过鸭绿江来表演的幼儿园，把我们同行的人给震住了，如果当年日本北海道的农民允许到中国的蓝天幼儿园、史家胡同小学、北京一一中学参拜学习，肯定也会被伟大领袖领导下的中国震住。<br />
　　只是我们在离开幼儿园时，未曾料想接待方委婉地认为游客们赠送的礼品不够，大家一时陷入尴尬，导游亲切提示下，我先捐出一百，好歹凑齐三百元，这才宾主尽欢。――去朝鲜的朋友们切记，别以为朝鲜穷，出发前那些小商小贩向你们兜售的文具之类礼品就可以随便打发，你们参观的其实是蓝天幼儿园、史家胡同小学，想象一下，接受你们礼物的人，长大后不少人会像他们的中国同行一样，成为领导人、国企老总、专家学者。<br />
　　当然，就如中国第一代自由主义知识分子大都是红色子弟一样，1989年在平壤街头贴出大字报猖狂攻击伟大领袖的，也是朝鲜的红色高干子弟，甚至他们观念转变历程都与中国同行惊人相似，都是受不该读的书的影响：中国同志是读到了内部发行的西方反动名著，而朝鲜同志则是读到了内部发行的马恩原著。<br />
　　更当然的是，如果有一天朝鲜南北统一，他们肯定是最怀念“阳光灿烂的日子”的朝鲜的人，甚至朝鲜普通人也会怀念过去是“主体”主人翁的种种美好，因为对统一后的朝鲜人来说，肯定是另一种“事大主义”，这个“大”就是今日的韩国。朝鲜人过去的一切，无疑都将随着制度被否定而被否定，个人的记忆和价值只在用于否定和批判过去时才有意义。而对个体存在价值的否定，无疑是对一个人最大的否定，即使能呼吸到自由的空气，能拥有天壤之别的生存环境改观。被同胞集体加以同情和歧视――这是无法避免的――都是精神上难以忍受的伤害。今天，韩国汉拿院的许多“逃北者”才几年时间就开始怀念起朝鲜，虽然他们绝不愿也不敢回。朝鲜南北60年制度分野造就的心灵鸿沟，肯定需要远比60年更长的时间才有可能填平。就一个民族分离的悲剧而言，或许史上未有甚过朝鲜者。<br />
　　真的，内心里我经常也会像孔庆东一样深沉地热爱着朝鲜，希望朝鲜这个人类制度实验室能永久存续。这与不少中国专门研究朝鲜的专家们立场相同，理由和动机不同。对他们来说，强调朝鲜对中国地缘政治中的重要性，朝鲜必须永久存在，那是关系到饭碗和课题经费的问题，虽然人们喜欢强调，朝鲜是隔开美国人的战略缓冲，但朝鲜战争之前，朝鲜半岛的美国人，连军事顾问、外交官加平头百姓，总数只有几百。有中国某些专家在，即使朝鲜再“悍然”，中国也会支持朝鲜活下去。<br />
　　其实，朝鲜让很多中国人不满的“悍然”举动，想一想我们的过去，真没有什么更过分的地方。比如，我们的领导人前脚刚走，朝鲜就立即一个大鞭炮，造成像是与中国商量好的效果，当年赫鲁晓夫秘密访华，中国让他公开走，他那边前脚走，这边就万炮轰金门，美国人真以为赫鲁晓夫访华就是商量打金门的。当年蒙在鼓里的赫鲁晓夫受了不白之冤，但今天，至少没有人相信中国会支持朝鲜在家里不断添置“大杀器”。<br />
　　到了朝鲜的普通中国人很少还会热爱她，我同行的人里，颇有不少一听到“尤其是改革开放以来”的说辞，就会热切怀念“尤其是”之前美好岁月的人，然而，与陪伴的朝鲜同志混熟后，中国同志就迅速放肆起来，先是强忍着优越感以无限同情的姿态过问朝鲜人民的疾苦，接下来，再以过来人的身份谆谆教导朝鲜同志“还是得改革开放，不改革开放咋整哈”。我之希望朝鲜永存，理由狭隘残忍而自私，我不希望有一天中国又遭遇任何一个歪瓜劣枣的家伙都可以来教育你的命运。<br />
　　话说回来，我同伴中“修正主义者”热心肠的发作，见多识广的朝鲜同志会用“我们当然非常欢迎中国同志来投资”来巧妙地应对，通常，幼稚的“修正主义者”会欣慰地连连点头，那就好那就好，你看，朝鲜也要改革开放了。三十年过去，修正主义者们大都忘了，改革开放原本意味着逐渐削弱对全社会的强力控制，在中国，当时是靠清算文革、平反冤假错案唤起的凝聚力，来替代被大幅削弱的强力控制，不认错、不释放善意，稍一松绑只会导致整个社会在迅速释放的不满中瓦解，在青年大将金正云同志即将冉冉升起的朝鲜，如何具备这种可能？<br />
　　朝鲜需要中国投资倒是真的。明年是金日成主席百年诞辰的大庆，要花很多钱，而且金正日同志允诺要在平壤兴建10万套住宅充分提高人民生活水平。此外，尹浩镇同志为制造核武器的全球采购也要花很多钱。但朝鲜没有什么东西可以拿来换汇，军火、毒品、超级美元这些拳头产品的销路，因帝国主义封锁受很大影响。不但添置计划面临巨大财政缺口，就是社会正常运转也极为困难。<br />
　　当年的中国都渐渐难以为继，而朝鲜不像中国是闭关锁国的社会主义，她是经互会大家庭的一员，苏东剧变断后，朝鲜由于幅员所限无法转变为自给自足经济体，八十年代以来陷入每况愈下的经济困境。虽然，日本在朝鲜留下的工业体系北方远超过南方，纯农业国的韩国到了1970年代初才赶上朝鲜，但今天一个步入发达国家之列，一个步入世界最贫困国家之列。<br />
　　路过的朝鲜烟囱很少有冒烟的，企业开工率可以想见，而路边晃过的农田，虽已垦到马路边，但劳作的社员多懒洋洋拄着红旗磨洋工。据朝鲜导游说，今春的寒冷天气对春耕影响极大，谁知道朝鲜是否又将迎来一个饥荒年份。不过，在饥荒会教给人生存技能。当年苏联在1921-1923年和1932-1933年两次大饥荒后，活下来的人已习得了应对饥荒的经验，所以1946-1947年的第三次大饥荒死亡人数少了很多。饥荒不仅会教会老百姓许多知识，也能教会政府许多东西，中国在三年大饥荒后，出台了缩小独立核算单位、允许一定程度的“三自一包”甚至允许退出公社的“调整”，同时默许了黑市调节作用，朝鲜同样在大饥荒之后出台了一系列与中国几乎相同、但却被外界认为是“改革”的新政策。这些年来，朝鲜黑市和地下经济的逐渐发育，使朝鲜人大都拥有了饥荒之年自己养活自己的技能和门路。<br />
　　当然，朝鲜面临的问题是，无法像当年中国一样，在挺过大饥荒后，又可以逐渐往回“调整”、斗私批修，缺粮即意味着缺少往回调整的本钱。尤为严重的是，一个正牌儿的社会主义国家，国家掌控的工农业单位生产严重不足，相对政府掌握的经济资源，地下经济和黑市在多年默许下俨然已过份发达，权贵阶层与普通人的财富差距，早已不是按等级分配资源的产物，而是靠权力肆无忌惮市场抽头的产物。<br />
　　朝鲜远比中国当年更糟糕的形势，只好祭出中国没用过的狠招：国家掌握的资源越来越少，要收回游离于国家掌控的越来越多的民间财富，除了通过币值改革让老百姓手上的钞票变成废纸，别无他法。<br />
　　当然，仅靠币值改革是不行的，而且你不能每隔一两年就改一次，何况它还遭到了意想不到的巨大反弹，所以，在工厂农村上缴产品和利润越来越难和这种方式越来越低效时，领袖若想集聚社会资源，把好钢用在刀刃上，还得开辟更直接的途径，比如各级领导的提拔与上缴忠诚金的多少联系起来，则正常财政系统之外又别开新源。类似的，“先军政治”下，军队可以直接到工厂和农庄收缴所需产品，这种简单明快的霹雳手段也是中国没用过的。<br />
　　所以，如果中国人投资，则意味着招商成功的领导有了一笔预备役的忠诚金。<br />
　　不过，朝鲜虽然穷，但依然有值得在中国人面前骄傲的地方，我们美丽的朝鲜导游几乎每两个小时就会对比性地介绍一次朝鲜的制度优越性：和你们中国实行九年义务制教育不同，我们朝鲜从幼儿园到高中，都是免费的义务教育，我们每个朝鲜人都有免费的医疗服务。朝鲜不仅是义务教育的年头长，而且全民受教育水平也远高于中国。当然，朝鲜的医疗服务制度，只是比中国的现在优越，但并不比过去的中国更优越，在中国医院只有四环素、链霉素、青霉素，开一片阿司匹林还要走后门层层领导批字的时代，中国公民也能享受全民免费的医疗。今天的朝鲜与当年中国不同点在于，好药不需要开后门，送一点东西，医生就会告诉你附近某个华侨家里可以买到中国产的好药。<br />
　　当然，社会主义的基本特征是人人平等，只是有一些人更平等而已。那位在本部幼儿园长大、向我热情介绍朝鲜医疗制度的导游，另一位认真想了一下才对我的同事回答说她的父亲是个医生、但不小心愉快自豪地透露她父亲隔几个月就去一次中国的导游，就是属于更平等的那一部分人。我完全相信，与她们同属更平等出身的同学，在将来统一的朝鲜，一定会有人写下《客观评论朝鲜时代的医疗卫生体系》、《人人享有基本医疗卫生服务是个纲，纲举目张》之类的文章，热情讴歌赞美朝鲜的全民医疗制度，就像他们在中国的更平等的同志一样，这个国家曾给他们免费提供的，远不只是四环素、链霉素、青霉素。朝鲜的免费医疗、免费教育、“一弹一星”的卓越成就，与他们的自豪骄傲联系得最为紧密。<br />
　　我愿意理解这将来的一切。<br />
　　去朝鲜前，一口气又把下载的10多部关于朝鲜的纪录片看了一遍，甚至把韩国电影《抉择》再看了一遍。这部正面描写在韩国被判重刑的北方政治犯在狱中顽强抗争的电影，虽然你会觉得狱中斗争方式与中国过去同题材片子如出一辙，但比之中国电影的浪漫化加工，该片极为写实的表达方式，将这种监狱中的残酷性体现得淋漓尽致，是我近年看过的影片中最令人窒息的。片中人物时刻忍受着逼仄囚室的压抑、渴望亲人渴望自由的煎熬、狱卒的折磨，他们当中的大多数居然靠着互相鼓励和牢固的信念理想，几十年顽强地坚持了下来。<br />
　　《抉择》传递的痛感乃在电影之外。因为在我看，这部片子中描述的人最残酷的命运并不是忍受漫长的牢狱生涯，而是在忍受了漫长的牢狱生涯后，竟然获得自由，愿这些活下来的英雄，幸福地永远不知道真相。<br />
　　敬请关注《凤凰周刊》周宇的朝鲜报道。</div>

							
							
							
							
