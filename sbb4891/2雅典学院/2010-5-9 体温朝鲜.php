																											<div id="postmessage_606139" class="t_msgfont">　　　　欣赏金正日<br />
　　　　　2010-05-05 12:13<br />
<br />
罗军<br />
<br />
(世界都在骂金正日，在骂声中他和朝鲜屹立不倒，我就是欣赏）<br />
<br />
<br />
　　在当今世界领导人中，我比较欣赏朝鲜领导人金正日。<br />
<br />
　　欣赏他，不是因为他是独裁者，妖魔鬼怪，那都是西方媒体强加给他的。别忘了，我国领导人也曾经、包括现在，都被西方谩骂侮辱过。<br />
<br />
　　有人会说朝鲜那么封闭，不怪领导人怪谁。不对，朝鲜何尝不想开放呢，封闭朝鲜的不是朝鲜自己，是西方发达国家，他们封锁了我们几十年，封锁了朝鲜几十年，封锁了古巴几十年。我们块头大，挣脱了这种封锁，现在能跟西方国家平起平坐了。朝鲜和古巴个子小，没得办法，仍然被封锁着。西方国家不说自己惨无人道，反而骂朝鲜闭关锁国。其实朝鲜的历史和命运与我国相似，只要想到我们也曾经被迫封闭，也被辱骂为独裁，不管人民死活的国家，我们就能够理解朝鲜过去和现在。金日成，金正日，能把这样一个众人欺负的小国，峥峥铁骨地立起来，谁会昧着良心说不佩服？<br />
<br />
　　有人会说金正日花天酒地。你见过吗？那不过是西方丑化与其不同意识形态的国家领导人的惯用伎俩。美国众议院情报委员会说，“金正日透过印制超级伪美钞、出售武器及毒品、每年获得三至五亿美元，然后在港、澳等地购买德国奔驰轿车、法国美酒等奢侈品，供自己及亲信享用。多年来，金正日的个人财产积累已大约有四十亿美元。”我只能这样说，如果美国情报可信的话，萨达姆的原子弹就应该在伊拉克找出来了。如果美国情报人员算人的话，就不会在1993年指控中国“银河号”船只运送化学武器给伊朗了，也不会无耻地登船搜索我“银河号”了。<br />
<br />
　　我欣赏金正日，是他的国家井井有条。穷是穷了点儿，但人民安居乐业。这种“穷”，是被西方主流社会长期欺压，封锁，没办法改变的“穷”。朝鲜不象我们，地盘大，资源多，可以回旋腾挪的余地大，它就那么一块山地小国，能让那么多人顽强生存下来，实属不易。<br />
<br />
　　我欣赏金正日，是他有骨气，有作为。泰山压顶而不变色，金正日做到了。核武器硬是搞出来了。有人会跟着西方的口吻骂他们穷兵黩武，劳民伤财。可是，如果换个角度，换到我们六十年代四面楚歌的日子，想想当初“把裤子当了也要搞核武器”的豪言，想想“两弹一星”在我国是多么令人骄傲自豪，想想核武器奠定了我国的大国地位的事实，我们就完全能够理解朝鲜，佩服金正日了。不容易啊！<br />
<br />
　　我欣赏金正日，是他堪称卓越。战略上，他能在大国之间调戏与反调戏。战术上，他能在时空之间沉稳应对，从未失手。先军政治，他并不是不了解会消耗资源，只是十面埋伏之下他不得不强军，同时军队也可以促进民生。我国解放军不就是很好的例子吗，战时可以卫国，平时可以生产、救灾、建设。对外开放方面，朝鲜一直没有停止过尝试，探索，“摸着石头过河”，他们也在摸。相信有一天，朝鲜会忽如一夜春风来，千树万树梨花开。<br />
<br />
　　我们很多人只看到朝鲜的困境，朝鲜的世袭，朝鲜的不许用手机，朝鲜的封闭，却不透过这些看到它的历史背景、世界冷遇、自主自立。每个社会都会有它的缺陷和问题。要看这些帐怎么算，是算到金正日一个人的头上，还是各个标榜自由民主却几十年联合封锁朝鲜的国家都领一份儿责任。朝鲜会有一天过渡到正常的国家形态，这也是金正日的期待。<br />
<br />
　　金正日最让我注目的是他那件灰色夹克，皱皱巴巴，既不亮丽，也不气派，说白了，穿着它有点象个糟老头。第一次注意到它，是2005年金正日到机场迎接我国领导人胡锦涛，他头发凌乱，穿着那灰扑扑的衣服，与我国领导人胡锦涛的风采形成鲜明对比。当时，我一边为我国领导人风度翩翩而高兴，一边纳闷，金正日怎么这身打扮呢？是装穷么，让全世界的镜头都发现朝鲜很穷，需要大力援助？是不屑么，中国领导人又怎么样，我就这身打扮寒碜你？是生病了么，所以显得如此憔悴不振？还是寒冷的冬天，他只有这一件灰夹克还穿得出去？<br />
<br />
　　都说有的政客会作秀。比如奥巴马访问中国，独自打伞步下舷梯。但是，我相信金正日，一如我相信我国总理温家宝，他们骨子里装着自己的人民和国家。我发现金正日那件灰夹克，不只穿那么一次，几年来都穿在他身上，在一些重要的场合屡屡出现，直到现在也没有丢。这让我想起我们的毛主席，周恩来，衣服上都是补丁打补丁，不是买不起新衣服，是恋旧，是朴素的作风养成了习惯。我也想起了我们的温总理，那件绿色冬衣穿了十年，那双开开合合多次的旅游鞋开胶了，修修又穿。金正日的灰夹克，让我相信，他也是一个朴素的领导人。也因此他在我眼里很伟大。<br />
<br />
　　那些骂金正日和朝鲜的人，扪心自问，你们为朝鲜人民做了些什么？能做些什么？朝鲜人民能指望你吗？金正日为朝鲜人民做的奉献，远远超过谩骂他的政客和跟着起哄的人。</div>

							
							
							
							
