我是农村人，在电视上看到了蓝翔学校的广告，去学习了挖掘机<br />
一下火车有接待站，你只要进了那个接待站登记报名，就别想离开他们的视线了。<br />
　　他们会派车把你送到蓝翔学校带你参观学校，你会被学校的占地面积和设施给征服的；然后催促你报名，我当时没带钱，先是交了100元的报名费后，然后把你送到了另外一个地方（等款处），在那里什么都做不了，只能坐在房子里给家人打电话要钱，交了学费才能去学校学习。（挖掘机没有免费试学，等款处也是免费试学基地，当然不是想电视广告上的给你实习设备，我估计也就是在教室里听老师吹牛，劝你交学费，，为什么安排在等款处，那里没有真正交了钱的学生，你无法得知真正在蓝翔技校学习的学生生活怎么样，学到技术没有）<br />
　　我当天交了学费以后（一共5780元:内含 学费5180元 + 生活费520元 + 被褥押金50元 + 床单被罩65元，减去100元报销的路费，不过最后他会叫你办毕业证要回来的，所谓的毕业证就跟小时候发的三好学生奖状一样，一文不值） 交完学费后他会给你一张一卡通和一个学员证，你的生活费就在卡里面，学校消费都是用这张卡；而且没有收学费的收据，你问他要收据，他会推脱说这么大的学校和设施都在这里，难道交了钱还不让你学，，等等，<br />
　　然后带你到了住的宿舍楼，宿舍楼环境很不好，12个人一间房子，每层楼就一个厕所，也是洗澡的地方；每次打完饭回宿舍吃饭，你都可以在楼道看见一个个光屁股在哪搓澡。<br />
　　然后去了理论课教室，老师并没有讲课，而且在和同学们讲笑话开玩笑；等到一个班的学生来齐了才讲课（一般1-3天，70人左右），教课的老师是20来岁的小伙子，其实他们讲的课，你看着挖掘机书也可以讲，他们的工资并不高，主要是拿提成.怎么拿法后面再说。<br />
　　学校的伙食更别提了，他们说7元一天的生活费，确实可以够，打7元钱的馒头就OK，你想吃菜，3元一份，米饭是1.5元，一顿饭4.5不是很贵，但是那菜非常难吃，我经常在里面看到苍蝇头发什么的，有时想吃点肉吧，去打了份8元的红烧猪肉，用筷子夹起一看 满肉都是毛，强忍着吃了，还是臭的肉，是夏天太热放坏了的那种味。<br />
　　学校的超市物价非常贵，外面卖5毛的雪糕里面1元，我学了2个月一共花了2000左右的生活费，不要以为是我不节约，是你在那里面消费就是那样。你想出去消费是不可能的，学校全封闭，管的很严。出去就别回来了，实际就是不让你在外面消费。<br />
　　<br />
　　在上了几天理论课后，老师给我们说XXX公司来我们学校要挖掘机司机，装载机司机，压路机司机去缅甸修高速公路，工期是3年8个月，工资是38万到-45万；他们只要复合型人才（会开2个以后上工程机械），意思就是我们想去就必须再学一个专业装载机或者压路机，有2个专业的是38万工资，3个的是45万，还跟我们说我们父母在家种地多不容易，来感动你，现在就是机会，回报父母的时候（实际他们就是让你交这个学费拿提成的）；一般来学这个的大多数都是农村的，也想毕业后有工作干，而且这次机会相当好，当然有超过半数的人学习了；其中我报的3项，又学习了装载机和压路机，学费一共4660 一项是2330。 (也是没收据的，而且几天后会给你一个学生证，上面盖着红色章印居然写的是免费学习！）。但是后来去了实习基地，听到比我们高一届的学生们也有这样的经历，说学双项技术的才给分配工作，让你交学费，才知道被骗了；根本就不是他们广告上说的那样，毕业后根本不直接分配工作。<br />
