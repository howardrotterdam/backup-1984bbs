<br />
<br />
1、蓝翔没有你们看广告以为那么简单。<br />
<br />
<br />
2、蓝翔确实有军方背景，一直和清华、哈工大、国防科大、上海交大、蓝翔五个学校承担了一个重大的国防科研项目，科研方向保密。这个项目不是随便可以参与的，清华是实力、哈工大是国防科工委的老大，国防科大是军校老大，所以才能直接进入，蓝翔是秘密基地，上海交大本来没份的，但因为校友的关系（就不用具体问谁了吧）加入了进来。我们主要负责电子干扰，网络超限战科研部分。这个不方面说得太细。北京大学、还有北京理工曾经申请加入进来，被拒绝。<br />
<br />
<br />
3、蓝翔有八个专业：数控、其实是高达控制专业。厨师、其实是化学武器制造与防御。汽修、其实是高达与重型战争机械制造。挖掘机、其实是异型高达进阶。美容发，其实是伪装与战略忽悠<br />
<br />
<br />
电焊，其实是激光武器与高能粒子武器设计。无线电，磁电声光波探测与预警系统。计算机，不做解释。<br />
<br />
<br />
4、计算机很强，但其实不是蓝翔的王牌专业，蓝翔的四大王牌是数控、汽修、挖掘、电焊，计算机排五到七位，但在国内高校里已经无敌了，顺便说一个银河计算机其实是我们造的，为了保密，我们送给了国防科大。<br />
<br />
<br />
