<font color="red">1%不赞同</font> <br />
<br />
最近，终于收到盼望已久的《来生不做中国人》和《中国比小说更离奇》。在此，感谢上海海关放行。第一时间读完，<a href="https://1984bbs.com/viewthread.php?tid=25287" target="_blank">依旧</a>激动万分。钟祖康先生99%的观点我都赞成，中国人确实劣质得不行，十几亿人像蛆一样逐臭而活、面目可憎、心理扭曲、自大猖狂、繁殖力强、毫无廉耻，是这个星球上最低等的贱种。<br />
<br />
唯独钟先生关于台湾的部分内容，以我目前的识见，实在无法赞同。<div class="quote"><h5>引用:</h5><blockquote>在《记台湾行》里，钟写道：<br />
<br />
“在台北的街头，倾情感受这个我觉得最有生气最有希望而又最被华人污蔑的华人城市”<br />
“台湾的发展是最健全、最可贵，最需要受到保护的濒临绝种华人社区。”<br />
<br />
在《中国式霸权主义下的西藏》等文章里，多次提到“台湾已经是讲究自由人权的民主国家”</blockquote></div>台湾俨然成为钟祖康先生笔下华人社会民主灯塔。我想台湾人看后肯定面红耳赤，实在是过奖。<br />
<br />
我不明白，以钟先生的深邃智慧，早已把巍巍五千年的垃圾文明批判得一文不值，又无情揭穿新加坡的假民主，为何对被中华食人文化深深浸淫的台湾如此情有独钟？莫非是由于，只有台湾的两家出版社敢出版贵大作？<br />
<br />
难道讲“台湾是民主社会”也成了政治正确？就像广大虚伪的知识分子假装关心农民那样？<br />
<br />
<font color="red">绕回原点</font><br />
<br />
2000年，朱宰相曾言：“<strong>台湾的民主是个笑话</strong>”。那时，我上初中，十足一个愤青，听到此番言论，也曾欣喜一番。<br />
<br />
后来，受美国之音等敌台毒害，知道“六四”之类情事，认为“<strong>中国要民主；无论如何，民主我们是要定了；土共，请给我们民主</strong>”，过犹不及，丝毫不考虑这个劣等民族是否有适合民主的土壤，不考虑这个JB国家的蛆民是否有起码的民主素质。<br />
<br />
依稀记得台湾中央广播电台的女播音员高喊：“美国是民主的堡垒；台湾是民主的灯塔”。那时我还幻想“三民主义统一中国”“自由中国光复大陆”，民主的光辉泽被我等化外之民<br />
呢。<br />
<br />
但随着我近年抽空看了一些书，闲暇时思了一些考，加之对中国人虚伪狡诈吹牛无知狂妄一整套独有德性的了解，我郑重认同朱F基同志的观点。<br />
<br />
<font color="red">嗜血国父</font><br />
<br />
孙中山，这个为了权力而不择手段的革命爱好者，至今仍被台湾方面视为“国父”，公开场合，其玉照高悬。观察孙那副冷漠、毫无同情心、无精打采的尊容，我实在看不出他有何值得被尊为国父的资质。<br />
<br />
与朋友讨论孙中山时，他给出了孙值得尊敬的五大理由：启迪民智、引进民主、人格伟大、关心实业、讨逆护国！<br />
<br />
～～嗒嗒～～清政府是否有新闻自由，我不了解。但我十分怀疑，一个像如今在美国的魏京生一样的角色会对国内的民众有什么重大影响？你在国外说得再凶有何用？《民报》满世界飞有何用？真正的战场在中国啊，为什么不回去干？怕坐牢？谭嗣同、邹容、章太炎等人为什么不怕？<br />
<br />
三民主义、五权分立更是让人笑掉大牙的狗皮膏药。一如陈炯明所言：“<strong>三民主义及其发表之政纲，类皆东抄西袭，绝少独立之思想，一贯之理论，而于国情亦未适合。</strong>”闭门造车本无可厚非，但却将中华劣质文化的诸如“民族主义”之类强加发挥，祸延至今。<br />
<br />
1896年10月，孙中山所谓“伦敦蒙难”被困清廷驻英使馆，成为其名声大噪的开始。但是，照《随轺笔记》的说法，孙中山是向使馆的看守下跪的、叩头流泪的、卖友求饶的，十足一软骨无赖。至于强迫党员效忠打指模、指示暗杀陶成章、逼反陈炯明，更是低劣人格的典范。<br />
<br />
作为一个可怜的“临时”大总统，没人服他，政令出不了南京，当然得让位强势人物咯。下台后假装染指铁路等领域，宣称“实业救国”，都是幌子。他从没放弃争权夺利。犹有进者，这位“国父”一再勾结外国势力，与袁世凯之辈卖国大比赛。最终，勾结到了苏联，联俄容共，同样祸延至今。<br />
<br />
有这样一位“国父”，台湾的民主质量如何，可思过半矣。<br />
<br />
<font color="red">介石暴君</font><br />
<br />
雷震、殷海光的《自由中国》，老蒋的“自由中国”，Which one do you believe ？<br />
<br />
<font color="red">勤快小二</font><br />
<br />
太子蒋经国七窍流血前，虚晃几招花拳绣腿，“宣布”开放党禁报禁，确实迷惑了大部分人。这位出身莫斯科中山大学、四十年代上海扮演“物价警察”骗走国人金钱、在台湾统领一切党政特务情治机构大搞白色恐怖的总统“继承人”，如今居然变成“<a href="http://www.bighan.net/bbs/redirect.php?tid=6998&amp;goto=lastpost" target="_blank">中国民主之父</a>”！真是滑天下之大稽！<br />
<br />
蒋经国瞎忙一辈子，勤快堪比店小二，比如开台湾东西横贯公路，他21次入山；连续五年拜访丰田西瓜村（蹭吃？）；四次窜访基隆奠济宫，并与住持下过两次棋……对人民爱惜失度，无故扰民，做秀不止。但是中国人就是喜欢这样<strong>巡游无度的勤政猪倌屠夫</strong>，一声声“总统好”恶心堪比“猫主席万岁”。<br />
<br />
蒋经国的一大困惑是：接班人选谁？早前培养蒋孝文，不料孝文恶疾成残；选蒋孝武吧，这小子暗杀江南不力，丢人丢到国外去了；其他的不孝子又没有足够威信，只好选一个没有儿子的软汉咯。<br />
<br />
<font color="red">无子歪嘴</font><br />
<br />
李登辉这个前共产党员，之所以能被蒋经国选中，很大原因是他没儿子，不可能搞家族统治！作为一个夹杂在强势人物之间的弱者，他只能在如履薄冰小施权谋，很幸运，他得到大内高手宋楚瑜的支持，一鼓作气，终登大位。至于“民主”，那是他破罐破摔。<br />
<br />
<font color="red">2000～</font><br />
<br />
陈水扁，我就不说了。马英九的当选，实在是对民主的讽刺。哪个欧美国家的领导人是靠脸蛋上位的？而台湾的选民就是这样趋之若鹜，以惊人的支持率把他推上来了，丝毫不考虑他的懦弱无能与装腔作势。长得好看（政治上无做为）居然成为一种优势，阿弥陀佛，幸亏这种民主只是诞生在台湾。<br />
<br />
<font color="red">“有选举而乏民主”</font><br />
<br />
按照《中国文化的深层结构》作者孙隆基的说法：“<strong>台湾如无族群矛盾，政党政治能否搞得起来，仍属疑问，目前是有选举而乏民主、多情绪而少理性。</strong>”400年前，福建广东部分混不下去的苦民迁至台湾，如今他们的后代成了“本省人”；60年前，老蒋的几十万大军败退至台湾，如今他们及他们的后代成了“外省人”。两拨人以浊水溪为界，日日声嘶力竭，蓝色、绿色。扎堆、小聪明、党派林立、相互拆台各种各样的恶行恶状令全球狂笑。<br />
<br />
民进党的党纲我基本赞同，但它上窜下跳、东西跳梁实在每每让我笑岔。民进党最大的问题是：它对国民党是有样学样，与国民党无本质区别，都是20世纪的苏联式硬性政党，与欧美式的柔性政党有天壤之别，毫无可能培养公平正义的优质公民。<br />
<br />
选举是目前台湾社会最重大的情事。这使台湾人心浮躁。谁擅长选举、谁滑头、谁嘴皮厉害、谁得到党派大佬的亲睐，谁就能粉墨登场，披一个写有自己名字的马甲。这样的制度，真正优秀的治国之才、有战略眼光的政治家很难出头。<br />
<br />
台湾的选民也都是典型的支那闲人。试问，哪个欧美国家的选举投票率像台湾这样屡屡拔高？每次一到选战，台湾人疯了，电视上满天选举议题，大街上狼藉不堪，看不到一个潜心做自己份内事的人。选一个大同小异的政客自己的生活就能好转？太天真了吧？真有闲功夫，还不如去养养花草，改善环境。<br />
<br />
中国人做什么事都是非做烂不可，独裁独得天怒人怨；选举选得换票、配票外加人头党员，过犹不及，一如中国人的其他德性，狗改不了吃屎。<br />
<br />
<font color="red">国会戏台</font><br />
<br />
立法院在台湾扮演的角色可有可无。由于所有追究狗官政治责任的弹劾权掌握在监察院手中；硬性党团操纵立法院预算拨款权；议员缺少虔诚宗教信仰而乏道德勇气……台湾立法院的功能就只有：为人来疯议员的即兴表演搭建平台。<br />
<br />
我们也会经常看到议员与行政官员捉对厮杀，在大庭广众下惊涛拍案。有理不在声高，叫得再凶有何用？人家又不是小孩，不怕你！毫无理性的攻击根本无法制服官僚。<br />
<br />
立法院的这113个议员如果真聪明，就应该努力争取修改虚伪宪法！强化议员个人独立责任！惹急了，干掉监察院考试院司法院，向欧美看齐，弹劾总统及其走狗！他们敢吗？不敢！有深入骨髓的中庸思想，他们只不过是<strong>高级奴才</strong>而已！<br />
<br />
<font color="red">顺便反一下华</font><br />
<br />
当然，我支持台湾独立，Why？因为中国越分裂我越高兴，不才乐见贵国四分五裂。<br />
<br />
台湾的假民主成为许多中国人兴奋的G点，幻想以台湾为样板，实现全中国范围内的民主。说实话，贵国实在不适合民主，还是老老实实遵守祖制，继续意淫神州华夏威震八方泽被万世吧。这是<strong>大家心知肚明的真理</strong>。</div>

							
							
							
							
