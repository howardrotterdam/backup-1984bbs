													<h2>方舟子遇袭几点疑问</h2>
						
						
																											<div id="postmessage_808574" class="t_msgfont">1， 遇袭过程的单方面描述<br />
先说辣椒水和迷药。辣椒水貌似是国民党刑讯逼供用的，迷药貌似是鸡鸣狗盗之徒入室盗窃以及迷女人用的。再说那铁锤。不知是不是那种擂鼓瓮金锤（为四猛八大锤之首，四猛八大锤一般为四名分别使用金银铜铁四种锤为兵器的武将的并称，是袍带评书或演义小说中常见的人物名号）。 <br />
<br />
既要致人死地，歹徒为什么不用刀具而用辣椒水或迷药，以及铁锤？<br />
<br />
铁锤一次砸头，不中；再次扔头，不中；再再扔，砸腰。于是腰部受轻伤。老方对记者说流了血，如果是锤击，淤青或是内伤有可能，流血不容易。为了证明自己确实被袭和确定自己身体没落下毛病，老方应该去医院认真查体。哪怕拍一张X光片，或者裸腰的照片，上传至博客或微博，也比在博客里写谴责、呼吁与抒情文字更有说服力。<br />
<br />
 2，遇袭之后紧锣密鼓或大张旗鼓的宣传<br />
<br />
老方被袭后不是第一时间去医院，而是第一时间给记者打电话担心他们的安危（见王刚博文）、给司马南打电话（见司马南博文）、让媳妇以媳妇名义代发微博（见方舟子微博）、通知各路记者、接多位记者采访，写微博和博客，第二天亲自组织召开记者见面会（见各大新闻网页及方微博和博客）。<br />
<br />
按正常讲，这个记者见面会不应该是方博士的一面之词。应由警方（作为第三方）和老方一起开，既然小区保安说有监控视频并且公安已经调看了视频，既然老方自己说法医鉴定了他的伤势，那么遇袭视频、法医鉴定结果、医院诊断报告、目击证人证词，都应该同时发布。最最起码，也通过视频让大家看看歹徒是什么样子，看看袭击过程，看看辣椒水、迷药以及铁锤。把视频放在网上，让全国网民一起帮助人肉搜索，一起帮助拘凶，岂不方便？<br />
<br />
3，多次流血和多次流脑浆<br />
<br />
 另一位打假英雄司马南在接受记者采访时说：“很无奈，也很同情，我也是多次遇到这种事情，也多次流血。”<br />
<br />
一个@啊普朵啦的网友在司马南这段话后面跟帖说：“司马昭说：很无奈，也很同情，我也是多次遇到这种事情，也多次流脑浆。”<br />
<br />
 4，打假与打假的区别<br />
<br />
揭露假学历、假魔术、假憋水道士的人，与追索王家岭死亡矿工名单、汶川倒塌学校名单的人，都是要真相，但还是有区别。方舟子说这次遇袭无私人恩怨，而是触动利益集团。其实前者的揭露往往演变成私人恩怨。而后者的揭露才是触动利益集团。<br />
<br />
都是打假和要求真相，前者和后者最明显而直观的区别是：前者博文总是上头条，接受各路媒体采访。而后者博文总是被删除，以至于博客最后被悄悄封杀。<br />
<br />
 <br />
5，肖传国<br />
<br />
 与方舟子有“宿怨”的武汉市协和医院泌尿外科主任医生肖传国，站在一个医生角度认为方舟子可能是在报假案、是在炒作。他说2007年2月1日，方舟子即报过假案，是以接到恐吓信为由，在鲁谷派出所立的案。肖传国表示，自己掌握了“百分之百的证据，包括各种录像资料、图片资料”，证明方舟子在2007年报过假案，“假案报了第一次就有第二次，所以我认为这次的也是场闹剧。”<br />
<br />
 肖传国的这些话，如果没有相关证据支撑的话，我也会认为是假的。<br />
<br />
 6，我们的已知与未知<br />
<br />
 李一可能有夸大其词和自我神化的一面。怕是方舟子司马南也有夸大其词和自我神化的一面，尤其这次遇袭。我在这里只是要证据，证明他们没有夸大其词和自我神化。证据摆在那里，真理不言自明。<br />
<br />
 我从没有迷信过李一，大家一窝蜂批判他，我才看了他的一段养生视频讲座。我认同他的一些养生的道理。我相信以王菲、李亚鹏、潘石屹、马云、樊馨蔓以及凤凰卫视刘春、杨锦麟、窦文涛、梁文道、王鲁湘等等的智商，不会被都为他所骗。也都是吸收他们认为有益的、可借鉴的、能够修身养生的部分。<br />
<br />
包括气功，包括特异功能，包括中医中药，包括我们对万事万物的认知，对整个宇宙的认知，对我们自身肉体和精神的认知，等等等等，一切我们以我们目前的科学所无法解释的东西，我们都不应去简单的否定。樊馨蔓的一段话我认同：“盲目地追从、崇拜，将自己不理解的事物看作为神灵，是愚昧的；而不经过调查，不经过了解，更不进行研究，一概将自己不理解的事情归作为迷信、荒诞，我认为也是同样的愚昧。与前一种愚昧没有太大本质上的差别，只是方向的不同。”<br />
