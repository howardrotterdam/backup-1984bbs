忐忑不安地期盼了好久，终于在一个周五的下午，收到了公安部方面的电话。语气挺友好的，比我想像中的要好。对方问问拿传真号码，说是要把回复传真过来。<br />
<br />
我当时没有传真号码可以提供给对方，就问他们可否电邮？他们说不可以，因为没有相关的设备（没有扫描仪？）～我又问可否邮寄？他们也是说不可以，说他们不搞这种形式的回复，因为这样需要申请人去银行汇钱支付成本等等，很麻烦的。<br />
