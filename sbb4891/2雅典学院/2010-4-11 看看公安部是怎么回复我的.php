<br />
到了周二（清明假期完了），我回电话过去，谁知道打了三次都没有人接。估计是对方假期比较长吧。那我周三再打～电话响了两下就有人接了，对方依旧语气有好。我把传真号码给了他。然后我飞快地搭地铁去复印店。<br />
<br />
 <br />
<br />
在复印店问女店员半小时前是否有传真发过来，她问了我的名字。当她听到我的回答时，眼神中有种说不清楚的东西。然后她很快地就把纸张拿给我，像是在扔掉些什么不吉祥的玩意儿似的。<br />
<br />
 <br />
<br />
看来一眼，我就把传真塞进包里，赶快回家了。回到家，仔细地看了几遍。很失望。虽然我早已经做好了心理准备，但是失望还是不可避免的。<br />
