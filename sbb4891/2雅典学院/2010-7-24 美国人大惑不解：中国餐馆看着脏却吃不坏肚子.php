													<h2>美国人大惑不解：中国餐馆看着脏却吃不坏肚子</h2>
						
						
																											<div id="postmessage_756284" class="t_msgfont">作为一名西方人，我对中国的第一印象是明显缺乏卫生，特别是在饮食上。在中国的菜市场，你可以看到猪肉一整天被摊在桌子上，既不包装也不冷冻。蔬菜也被铺在脏兮兮的地面上，与烂菜堆相距咫尺。苍蝇和其他害虫到处飞舞。中国的餐馆更糟糕：地面脏，炊具常常是旧的、生锈的，厨师切菜、切肉都在同一个地方。用西方的标准看，中国的餐馆和市场远远到不到我们对卫生的期望。<br />
<br />
我想这主要有3个原因。首先，中国是个发展中国家，冰箱的使用相对较晚。尽管如今大多数中国人都有冰箱，但类似的电器完全融入文化还需要时间。冰箱在中国仍是件舒适品，而在西方则是必需品。肉类和蔬菜的包装同样可以这么说。<br />
<br />
其次，美国人过于担心食品卫生。我们的洁癖社会把太多时间和精力用在了确保我们的食品干净、密封、新鲜、不受污染等上面。没错，细菌是大多数传染病的罪魁祸首，但别忘了我们每个人的身体都有免疫系统。不是说食品卫生不重要，而是说美国做得过头了。<br />
<br />
最后，我真正想说的是：中国的传统烹饪方法要比西方的卫生。中国社会绵延近5000年，但没有“现代的”卫生技术，这说明其饮食文化在卫生方面有独到之处。<br />
<br />
让我们看看几个例子。<br />
<br />
中国人很少用手碰食物。我们担心喷嚏、吐痰，但其实身体没有哪个部位像双手一样藏有许多细菌。而西方的饮食中相当一部分还是“手抓食物”。这在中国很少见。即便像花生这样的小吃也都是用筷子的。如果你在中国的快餐店买烤鸡肉，他们一般会同时给你塑料手套，让你享用时戴上它。为什么？手少接触食物，细菌进入肚子里的机会就少。我不是说中国人自古就懂得细菌理论的知识，但是，不用显微镜都知道用手接触食物有可能会得病。<br />
<br />
在中国，蔬菜都是做熟吃的。大多数中国人都不会想到吃未煮过的蔬菜。无论是爆炒还是在滚水里烹煮，中国人吃的蔬菜99％是做熟了的。吞食新鲜蔬菜经常导致病菌吃进肚子，吃煮熟的蔬菜就没有这样的问题。<br />
<br />
中国人习惯吃小块肉。在西方，我们喜欢大块的肉，切成小块是我们在吃饭时才做的事情。而在中国，这个任务都是由厨师完成的。肉在下锅前都被切成一小块一小块的，这样能确保更容易煮熟。在西方，我们喜欢把大块的肉煮熟，然后由食客自己动手切。这样的做法有可能造成肉未被完全煮熟。<br />
<br />
一切食物都经过火。中国的厨房也许是脏，但端出的菜都是经过极度加热的。人类文明已经证明没有比火更能杀灭细菌的方法了。与西方用烤箱不一样，中国的厨师用炒锅做菜时可以不停地翻动食物，从而确保所有的菜都得到加热。除少数例外，中国的食物永远都是热乎乎地被端上桌面的。而且，中国喝的水都是煮开了的。<br />
<br />
有趣的是，在我待在中国的这3年时间里，我只有一次吃坏肚子；相比之下，我住在美国时，平均一年至少一两次吃坏肚子。乍一看，生活在一个食品卫生管理松懈的环境里，会导致与食物有关的胃病的增加。但在中国的3年饮食经验表明，事实不是那么回事。（作者［美］Benjamin 编译管锥）<br />
