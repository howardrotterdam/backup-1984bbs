																											<div id="postmessage_757798" class="t_msgfont">脑残到连喝个开水都觉得自己文明进步的很了，还有什么可说的呢，转一段历史看看<br />
<br />
<br />
1843年。上海开埠，霍乱病毒由西方人和印度人群传染至中国，同时西方人也对中国人的卫生习惯担忧<br />
<br />
一位当时的法国人这样描述：进入上海小镇，用扁担挑着敞开着的木桶的男子便迎面而来。他们是大粪搬运工，沿着固定的路线穿过城市。倘若跟随这些掏粪工，你会发现，他们走到附近的沟渠两侧，将木桶里的污物哗啦一声倒入敞舱驳船或另一种船舶里，污满为患时，船只便被牵引到乡间的稻田里。废物被胡乱倒进水中。沟渠之水少有流动，至少还不足以清除绿色淤泥，改变浑浊发黄、满是污秽的水质。可就在这条船旁边，人们正舀水来饮用和烧菜做饭咧。<br />
<br />
初来乍到的西方人，对在这里的生活状况，充满了担忧。不久，这种担忧就化成了现实危机。<br />
<br />
1849年，英国人终于发现，蔓延的疾病和被污染的水，以及糟糕的生活密切相关，尤其是在破烂不堪、居住拥挤、卫生条件极差的城市环境中，疾病会更迅速传播。这一切迫使政府必须提供和改变公共服务。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 1845年，英国的一份官方报告首先建议各地要有单一的公共卫生主管机构，负责有关排水、铺路、净水、供水等工作，并且要求主管当局规范新建筑物的兴建准则。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 1850年夏，一份英国传教士的医学报告记录，这一年的5月～7月，上海人被一种怪异的肠道传染病所困扰，多人死亡，几乎所有居民都在戴孝。这一年，上海刚刚开辟了通往伦敦的航线。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 1853年，在拿破仑三世统治下，法国开始了由奥斯曼（Haussman）主导的巴黎重建计划。这时的欧洲人来到上海时，发现这座城市的气质和英国伦敦颇为相似。不止是因为这两个地方有着同样的湿润气候，还因为这里，与伦敦同样有着一条安静且不洁的河流――在伦敦是泰晤士河，在上海就是苏州河。19世纪的五十年代前的工业化为泰晤士河带来了大量移民，也带来大量生活、生产污水，整个伦敦市区臭气熏天。经历了霍乱时期的阵痛，英国人下定决心治理不洁的泰晤士河。刚刚发生过的惨痛教训让欧洲人对于糟糕的上海生活习惯时刻充满着恐惧和不安。他们在苏州河看到上游倾倒污物，下游舀水、做饭的不堪景象，到了后来，霍乱引起了英国侨民死亡。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 1860年开始，英国实施一系列改良贫民窟的计划。当时，有欧洲人侨居到上海开始按欧洲样式改造上海的生活环境，这是上海近代化运动的开始。外国人在上海老城厢的旁边建立起了一座“近代化的新城”。这座由外国人控制的城区不断扩大规模，并将西方的观念、制度以及制造业引入中国。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 1862年，创建于1797年，位于上海县城外西北侧紧邻护城河处的上海四明公所被划入法租界内，四明公所是宁波人的同乡会馆及公坟，旅沪的宁波人死后大多停棺公所，再择机运回家乡安葬。随着时间的推移，公所内浮棺厝厝，每到夏季，臭腐蒸发，无疑是传播疾病的巢穴。<br />
&nbsp; &nbsp;&nbsp; &nbsp; 一艘名为“千岁丸”的日本帆船载着51名一心想打通对华贸易之路的幕府官员和商人，从长崎启航来到上海。然而，他们中的一些人还未见识他们心目中的“西洋”上海的繁华，就上吐下泻起来，不是菌痢，而是霍乱！三天内，三名日本人死亡。<br />
<br />
1863年，霍乱肆虐之势不仅未见减弱，相反愈加凶猛。租界也笼罩在瘟疫之中，驻上海的外国人中不少人死亡，其中包括外侨中的一位显赫人物――江海关总税务司，英国人德都德。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 1874年，西方殖民者对中国人生活习惯的不满集体爆发。在中国人看来，那是外国人对于中国领土有预谋的一次侵犯。冲突的诱因是与法租界毗邻的四明公所。当时的法国公使数次向上海道台抱怨：以大都市之上海而论，在距黄浦滩一里之内，即可嗅得腐败尸体之恶味……而那时，上海道台的态度暧昧不清。一方面，宁波商人是上海势力最大的商帮，在当地政界举足轻重；而另一方面，“以商制夷”的外交策略，也让那些政府官员甘愿退居二线。<br />
<br />
屡屡抗议无效后，法国人决定修筑一条穿越四明公所的道路来迫使其迁出租界。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;事实上，起先中国人对于现代化城市的进程，还乐意接受。但这一次，事情因涉及了中国人极为看重的身后事而起了变化。宁波人的同乡会自然反对筑路计划，这里放着旅沪打拼的宁波人尸骨，随意搬迁就是对祖宗的不孝，就是死无葬身之地。他们向法租界公董局（即法租界工部局）提议，将道路稍为向北迁移，并且愿意承担筑路费用。但法国人的真实目的并不是修路，而是要躲避传统习惯背后的致命疾病。<br />
<br />
5月3日下午，300多名宁波人在四明公所门外与法国巡捕发生冲突，随后冲向路政工程师佩斯布瓦的住宅，后者向人群开枪射击，一名中国人中弹身亡。事态扩大到华洋冲突的边缘。宁波人包围了公董局，焚毁法国人房屋40多间，法国水兵上岸镇压，打死中国人数名（死亡人数被租界当局隐瞒），伤20多人。<br />
<br />
法国领事和上海道台都不愿意引起更大的麻烦，最终法国人做出了让步，放弃这项筑路计划，四明公所原地依旧。<br />
<br />
1898年，时间过了24年，法租界的发展突飞猛进，四明公所已经成了都市里的坟场，令人侧目。不发生疫情的时候，一切都好办，到了传染病爆发的当口，这里注定又成为冲突的焦点。<br />
<br />
这年，上海发生了鼠疫，死人无数。法租界公董局重提筑路一事，再次与宁波同乡会发生冲突。这一回，法国人率先打算与宁波人谈判。法国领事白藻泰找来了精通英语，很会办事的洋行买办宁波人虞洽卿，希望虞能够从中沟通、斡旋。提出愿意以高价收购四明公所的土地。<br />
<br />
双方的谈判最终破裂，全城的宁波人抗议，之后引发冲突导致了5位宁波人身亡。<br />
<br />
宁波同乡会一面开追悼会，一面发动大家不再为法国人服务。虞洽卿说：凡是为法国人服务的宁波人和其他中国人，你们的工资由我们四明公所出，不要为他们干活，直到法国人向我们“赔礼道歉”为止。<br />
<br />
那时候的上海是宁波人的上海。他们人数众多，而且充斥于各行各业，不少握有实权。怀着淘金梦的宁波人几乎控制了这个城市的服务行业，比如当时的洗衣大王沈洪赍，一声令下，全上海的洗衣人，就都不给法国人洗衣服了。<br />
<br />
《新闻报》和《申报》刊登了法国人为争买四明公所地皮造成流血事件的新闻。随即，学生和商界都起来响应，口号是“中国的土地上不许法国人杀人。” 接着，宁波人发起游行示威，包围法国领事馆。这类抗议无异于弱者的悲鸣，法国领事馆的损失是，玻璃窗被石头砸碎。<br />
<br />
中午时分，法国领事馆里的中国厨师都不见了，法国人只得咬面包。晚上断水、停电。第二天，厕所也没人打扫了。送去换洗的衣服，店主一听是法国人，立马摇头。<br />
<br />
最后谈判是双方都做出让步：法国人答应保留四明公所，中国人承诺不再往那里放新尸体。<br />
<br />
四明公所事件之后，住在上海的中国人开始决定试着自己把上海建设得像租界一样干净整洁――这更多地是出于对于洋人租界发展的遏制与反弹。西方人对于中国人的卫生影响还是潜移默化地进行着。<br />
<br />
l870年，即将上任的公共租界卫生官爱得华・亨得森博士号召在上海来一场接种牛痘的运动。但是，中国人对于西医充满恐惧，来自民间的反应却是寥寥。于是，公共租界的工部局公告，愿意接种的小孩每人给300文的赏钱！此后，愿意接种的孩童才逐渐增多。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 1905年，清政府倒试图建立过一套从中央到省级的卫生行政系统，甚至州县的卫生事业也有专人负责。但由于随后清朝迅速覆亡，这些措施未及实施。<br />
<br />
<br />
1928年至1930年，中国发生酷旱。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 1931年，中国大部分地区发生洪灾，饿殍盈途。同年“九一八”事变爆发<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;1932年，公共租界流行霍乱时，工部局规定，每报告1例霍乱，酬银l两。而这一年，中国大规模爆发饥荒和霍乱。夺去了许多饥民生命。1月3日日本人攻陷锦州，东北沦陷。月底，“一二八”事变发生，日本向中国南方重镇上海发动进攻，国民党军队发动第一次淞沪抗战，3月14日双方停止军事行动。中国大部分地区战事停息。即将席卷全国大部分地区的瘟疫于此时悄然登场。<br />
<br />
当时中国的卫生体系，尚未建立。全国的死亡人数没有专门的统计。国民政府海港检疫处兼办的中央霍乱局的“估计”是，霍乱侵入23个省，312个城市，病者10万人以上，死亡约34000人。<br />
<br />
这个“估计”很保守。<br />
8月15日《大公报》称“关中虎疫已蔓延42县，死亡数有报告者达22000余人，西安日死百余人”，<br />
9月6日《大公报》称山西死于霍乱者已达10000多人。<br />
8月10日《大公报》报道“洛阳疫炽，死者10000多人”。<br />
仅陕西、山西两省加洛阳一地死者已42000人以上。<br />
<br />
中国的霍乱年代降临，情形与上个世纪的欧洲惊人地相似，人们在黑暗时刻对于灾难的描述也如出一辙。<br />
<br />
在山东，“哭声遍野，吊旗飘飘，棺木售空，因惧传染，无人敢任葬埋”；<br />
<br />
在江西，“棺木出售一空，死亡逃避，乡村已断人烟”……<br />
<br />
如今，位于黄浦区人民路830号的四明公所只剩下一座牌坊，淹没在周围的大片楼宇之中。它之所以留存到今天，是因为它作为反帝爱国运动的纪念地被存进了历史。</div>

							
							
							
							
