																											<div id="postmessage_469171" class="t_msgfont">回家乡的恐惧－－－－-中国部分县城的现状 ( 转帖 )<br />
　　<br />
　　<br />
　　一、并非无端的恐惧<br />
　　　　　　<br />
　　　　　　因家事，我于 6 月 27 日 赶回广西北部的全州县。火车在晚点一小时五十分后于 28 日凌晨一点多到达全州车站。下车的人不多，出得站来碰到用小面包与三轮机动车（在我家乡叫 “ 慢慢摇 ” ）拉客的却不少，把整个车站前的广场塞得只有一条可过车的道。他们大声招揽着客人，也为争拉客人而互相责骂着。<br />
　　　　　　<br />
　　　　　　全州是个有 80 余万人口的农业大县，近十来年曾经的几家国有企业已全部倒闭并被变卖。县城除了商业、服务业，基本没有其他产业。这样一个县城，车站拉客自然就成了许多人谋生的选择。竞争激烈也可以想见。如此一来，此地也是各派势力逐利之地，各种矛盾都集中突出。<br />
　　　　　　<br />
　　　　　　我们一行五人（三个大人，两个小孩）是出站中最大的客户群，自然引得拉客者的追跟。在努力摆脱拉客者后，我们因座车太久而决定先在站前小饭店吃点东西再走。我发现那些小店青一色都挂着我们邻县灌阳县饭店的招牌，我很奇怪怎么本县没人来开饭店，反是邻县来开店？进得一家店后我说出了疑问，谁知那老板说： “ 这是挂羊头卖狗肉，其实都是本县的饭店，因为灌阳路远，来这里等车并在此吃饭的人多，所以饭店为招揽生意而都改名为灌阳饭店。 ” 接着他似乎意犹未尽地说： “ 本县都难经营下去，外县来怎么能经营。 ” 我闻听此言，马上追问一句： “ 是生意难做，还是干扰太多？ ” 那老板似乎马上意识到失言一样，于是随声附和着： “ 都是，都是。 ” 我也感到自己所问含糊，但又感到饭店老板似乎明白我所指。<br />
　　　　　　<br />
　　　　　　对家乡全州近年来我常听人谈及社会治安状况恶化，小小县城吸毒赌博成风，黑社会势力嚣张，各种偷抢、打杀、欺行霸市情况严重，民众在街上不经意侧目都可能招致一顿拳脚，甚至刀棍，百姓普遍感到人身无安全保障，生活在惶恐之中。我所指饭店干扰多就有受社会黑恶势力影响的含意。显然在全州说到干扰大家似乎都心照不宣地理解到黑势力上。<br />
　　　　　　<br />
　　　　　　正在我们吃饭时，进来两个青年，在店中转了一圈就出去了。过一会又进来一个青年要了一点小吃在那慢慢地边吃边东张西望。对于在社会底层漂荡了十几年的我，一看这情形就感到不对头，本能地警觉了起来。我一面用当地话催促大家快吃，一面漫不经心地问饭店老板车站派出所有个亲戚他是否认识？那老板说不熟，而那吃饭的一听就起身走了。老板似乎有意无意地自语了一句： “ 是当地人应该不会动手吧。 ” 我想这些要钱不要命的可能不一定顾忌当地不当地，于是我们赶紧结帐离开饭店，到外要了个面包车往县城里赶。<br />
　　　　　　<br />
　　　　　　车站到县城约有八里路。三年前我回去时这段路车费是 1 。 5 元，包车是 5 元钱。现在一问价钱涨到 2 元一位，包车他开价 20 元。我们座上车后本以为他会一路到县城，结果他居然在出站口又让一个青年上车，那人上车后就爬在车上不知真睡还是假睡。走出不到一里，居然又有一批人在路边嚷嚷，他们大约是十来个二、三十岁的青年，全部光着膀子。一人上车来看了看，见我们是一家，后边还有一个爬着的，就没再上车。又走了两三里，在漆黑的路边居然又有一个人来上车。当时应该快凌晨两点了，我本来紧张的神经一下膨得更紧。凭经验我认为这不是巧合，于是我不断与家里人对话，提醒他们注意。这两个先后上车的人没有跟司机说到哪里，司机居然也没问一句，就一一默契地让他们顺路上来了。我想这种情况如果出事，那司机肯定跟他们是同伙。<br />
　　　　　　<br />
　　　　　　好在座了一段路进入城里时，那两人就先后下车了。我终于松了口气，并气愤地质问司机怎么中途还搭人。那司机居然毫不示弱表示车没有让我们包。我只好忍气吞声，不管怎样终于平安回到了家。我的紧张引起了家中亲人的埋怨，认为我过虑了。然而过了几天一个在县城的远亲碰到我，他跟我说他听到有几个混混（当地流氓的称谓）前几天差点对一家从外地回来的人下手，幸而发现是当地人，否则不会放过。忽闻此言，我当时惊出一身汗，当然我希望那一家所指不是我们。<br />
　　　　　　<br />
　　　　　　我从京回到老家，居然有如此紧张以至恐惧的感受，显然不是纯粹的胆小。经验告诉我身边潜伏着太多危险。当然我无钱供他们偷抢，但这些作案者却未必领会对象的无钱。<br />
　　　　　　<br />
　　　　　　可见恐惧离我们多近。我这样在外闯荡多年并且外表看来也让山乡小民可能误解为有权势的人，也面临这种安全的威胁，可以想见一般平民百姓与外来客商的安全感会有多少。<br />
　　　　　　<br />
　　　　　　<br />
　　　　　　<br />
　　　　　　二、黑社会、警察、政府<br />
　　　　　　<br />
　　　　　　我们县下面龙水、大西江两个乡的大山中有锰矿，本来国家明文禁止私人开采，但在这些地方大量存在私人开采情况。这本是很容易查的事，但当地执法部门基本上睁只眼闭只眼。原因是此中大多数的矿井有执法部门作后盾，有的甚至直接就有执法部门的股份。当然也有另外一些个体老板去开采的，那么这些通常就是警察抓捕的对象，然而警察也常常网开一面，只在一些重大节日，如春节前才在有内线掌握了充分情报后出击，抓一个矿老板就罚他三十到五十万元，交钱后就放人，放了后又去挖矿。这样一来，矿山就成了当地警察与执法部门的小金库了 ―― 私自参股渔利，或公开执法抓矿老板罚款为单位创收。<br />
　　　　　　<br />
　　　　　　在当地把持这些矿山的就不是那些只有钱的，而是全县有名的黑社会势力。在龙水与大西江的所有矿山，都是直接由县城黑社会势力出人去保护，名为防当地村民抢矿，实则就是收取高额保护费。而这些黑社会的后面就是警察。<br />
　　　　　　<br />
　　　　　　今年 6 月上旬，为了争取收保护费的权力，全州新起来的黑社会势力，号称 “ 十二少 ” 与原来掌控矿山保护权的老黑社会势力 “ 谢老三 ” 团伙发生了冲突，最后约定在县城边上的一个桥头展开决战。一时双方上百人卷入战团，最后以老势力首领 “ 谢老三 ” 被砍倒，十几人重伤入院，新起的黑势力获得主导权而告一段落。<br />
　　　　　　<br />
　　　　　　同样在 6 月中旬，又一次全州新起来的 “ 十二少 ” 受当地一派出所领导的指令，召集了二十号人，在县城一家大酒店门口，公然将另一黑帮势力近八十人砍散，并当场杀伤十余人。事后 “ 十二少 ” 被警方通知避离县城近半月，等事态平静后又返回了县城。<br />
　　　　　　<br />
　　　　　　在县城北边原城市菜蓝子基地 ―― 小菜园，随着社会发展，城市扩建的需要，县政府曾以公益事业需要的名义动员村民同意征地。后来政府又将征来的地出让给房地产开发公司，这惹得村民不满，进而招致一批村民的抵制。当地政府为了平息事态，消除民众对施工的干扰，竟然动员 “ 十二少 ” 出来为其干事，为他们提供办公室，给他们工资报酬，并给他们一些工程承包。如此利用黑势力来消除民众抵抗。<br />
　　　　　　<br />
　　　　　　<br />
　　　　　　<br />
　　　　　　三、没有安全感是一种普遍的生存状态<br />
　　　　　　<br />
　　　　　　这次回乡感触很深是听到普遍感叹没有安全感。在家财物没有安全感，随时有可能被偷，入门偷盗情况在当地很严重。出门人身都没有安全感，一不小心可能被人偷抢，或被打。<br />
　　　　　　<br />
　　　　　　我回到家的当天，在县城我妻子的姐来看我们，进门不久就聊到社会治安差，经常有人早上出去跑步时都被抢，有的女的戴的项链与耳环在街上公然被抢，并因此把耳朵与脖子撕拉得鲜血淋漓的都有。因此她一再告诫她妹妹别戴什么首饰上街，尤其晚上不要在外，若晚上在外十拿九稳要出事。她说自己就被人抢过。在她说话的神情中都流露着一份惊恐。<br />
　　　　　　<br />
　　　　　　同样我到城郊一个叔叔家去探望，在与他聊到社会一些情况时，他感触地说这个社会现在连人的生命都没有保障了。听到这话我是吃惊的，因为我这个叔叔在我们当地是有名的会点功夫的人，他曾独自徒手空拳打倒过三个与他一般高大的流氓青年，以致在当地传为佳话。现在连他都感慨没有安全感了，可见事态的严重。我问他为什么这么感到不安全。他说： “ 现在社会这帮青年不知怎么回事，一下不对就动刀子，并且成群结队，一打架就是十几人、甚至几十人的上，更让人惊心的是这种架还经常发生。我在县城卖小菜，就常见到这种打架的情况，那真让人害怕。也许我是老了，经不起风险了。 ” 其实我叔叔也才五十岁，应该不算老的。<br />
　　　　　　<br />
　　　　　　当我回到离县城三十多里山路的老家时，我的伯母跟我流着泪诉说： “ 我辛苦养的八只鸭正准备去卖时，前几天被人骑摩托车到乡下来用麻袋装了去。村里有人看到两个青年来抢的，但不敢去追，甚至不敢近前。因为他们手上有刀，并且坐着车。留在村中又都是些老人孩子，谁经得起他们打。后来我们报了案，也没见有什么结果。 ” 我赶忙问： “ 这种事以前发生过吗？ ” 她说： “ 近年来这种事常发生。村中养的狗也常被人偷了去，现在连狗都养不起了。这些强盗常常是明目张胆地开着摩托车来，碰到什么就盗什么，毫无顾忌。若有人敢去阻止就还得招顿打。 ” 我问： “ 难道说当地政府就一点不管吗？ ” 她说： “ 他们管个屁！那盗狗的有的就送到了他们嘴里，他们完全是一伙的。 ” 闻听此言，我无言以对。在这边远的山村都难求得一份应有的安全感了。中国重灾之下是不存桃花源了。<br />
　　　　　　<br />
　　　　　　四、免税之后话农民<br />
　　　　　　<br />
　　　　　　中国中央政府给农民免去一切农业税后，我原以为这次回家可以听到不少欢呼了，或者还能感受到上世纪八十年代初那久违的农民的欢欣。在跟农民亲戚交谈后，结果却让我大出意外。<br />
　　　　　　<br />
　　　　　　6 月 29 日 ，我携妻带子去了岳丈家 ――鹅塘里村。该村离县城十二里地，不是边远农村也不是城市边上，但自然条件在农村算中上等，因为灌溉条件很好，人均也有近两亩田地，农作物运到县城贩卖也还方便。如此条件，在农业税免掉后应有个直接的感受。然而我在跟孩子的舅吃饭时就直接问： “ 现在应该没什么农业交税负担了，农民该好过了吧？ ” 他说： “ 农业税是不用交了，但是农民负担一点没感到轻。因为农资贵了，如化肥、农药比原来都贵了。这些东西涨的价甚至比免掉的农业税还多。还有城里吃国家饭的（意指拿财政工资的）工资不断涨，物价也跟着涨，而农产品却不见涨，这样一来农民实际收入增长还是没有。近十几年来那些吃国家饭的条件在不断改善，哪个见过农村靠种田改善过生活条件的？那些农村家庭有所改善的也都是靠到外打工而不是种田。这样一来农业税作用有多大？当然免比不免好，但免税显然不是根本上改变中国农民状况的路子。 ”<br />
　　　　　　<br />
　　　　　　由此可见中国今天免农业税的改革绝不能跟上世纪八十年代初的包产到户改革相比。上世纪八十年代初农村的改革给农民带来的翻身感今天实在难以找寻了。我曾苦思过那个时代为什么能唤起农民那样的激情与欢欣，原因其一应该是农民劳动力的解放，由捆绑的集体劳动，到自主的劳动，这是一种大的变革。同时对生产资料土地的使用权的获得，激发出农民生产上的主观能动性；其二、农业科技的进步，杂交水稻的推广，使农村很快解决了吃饭问题。这种对自己劳动力支配权与土地使用权的获得的改革是有历史意义的改革，生活上由吃不饱到吃得饱的进步是个质的飞跃，就此而言那时候的包产到户是真正意义的改革，而今天的免税充其量也是种给农民的减负而已。在这点上，可以瞒天瞒地，但瞒不了农民的切身感受，农民的切身感受是对真假改革最好的诠注。<br />
　　　　　　<br />
　　　　　　五、没有盼望是农民最大的悲哀<br />
　　　　　　<br />
　　　　　　这次回乡，村民给我最深的印象是没有了盼望。记得上世纪八十年代初中国农村改革伊始之际，农民心中充溢着激情，对未来怀抱着憧憬，就那时的日常三餐来说应该还是没有今天丰盛的，直接可支配的外在物资也可能没有今天富足，但是那时的村民普遍精神是乐观的，心情也是开朗的，生活是带着阳光的。然而几十年改革下来，不知从何时开始，农村就失掉了已有的欢欣，农民脸上退去了那难得的笑颜，代之而来的是阴沉与愁苦，是灰暗与无望。<br />
　　　　　　<br />
　　　　　　说实在这神形早几年我回去时就有感受，但一直也弄不明白是怎么回事。跟村民谈论时，只是听他们叹息连连，说这也负担，那也困难。这次回去我就决定详细与他们谈谈，看究竟是何原因。这次我在两天跟十来个村民一块吃饭时就直接问他们： “ 今天感到是否顺心？对今天的生活是否满意？是否有高兴的感觉？对未来有什么盼望？ ”<br />
　　　　　　<br />
　　　　　　我从他们七嘴八舌的回答中可以听出几方面意思。第一、今天生活比以往是有所改善，日常吃的比以前丰富了，穿的也相对要好些了；第二、挣钱的机会比以前要多些了，就是应急时借点钱也方便些了。<br />
　　　　　　<br />
　　　　　　大家觉得现实的直接物资条件的确比以前有改善，但是大家感觉不出快乐。原因主要在以下几方面：其一、精神上被抽空、颠倒了一样。这个世界好象找不到有意义的东西了。金钱就是价值，一切贪官污吏、偷盗、娼妓都成了社会炫耀追求的目标，社会丧失了荣耻。人生这样生活下去怎么是个活法？尤其后辈儿孙在这种世风之下根本没法教育引导，他们要走向何处，实在令人忧心。每每面对这一切，人就感到生活没有了意思。<br />
　　　　　　<br />
　　　　　　这是个没有了精神的社会，乱搞一气就是一生。这是村民普遍的心态；其二、没有盼望。包产到户几十年了，温饱早解决了，生活条件也有所改善了，但是这些变化自上世纪八十年代就有了，后来虽有些变化但都很微小，没有明显的改观，甚至在许多方面条件还在恶化。农民几十年的奋斗也没有走出根本性改观的命运，还是处在社会底层，还是停留在上世纪八十年代的温饱，中国社会的改革发展，那些所谓强大与崛起似乎都与农民无关。城里高楼不断，当官的工资一涨再涨，贪污受贿的公布款项也几位数往上翻，但农民却原地踏步，这样的社会与这样的改革早已让农民没有了盼望；其三、村民负担加重了。虽然前面所讲挣钱路子相对多了些，但用钱的路子似乎更多。挣钱远没有需要用钱涨得快。教育一提高收费，农民送孩子上学就难了，增加的赚钱机会远没有增加的支出钱的机会多，而辛苦劳动增加的收入也远没有增加的开支大。上世纪八十年代一户农家基本可以将子女送到他考不上学为止，不管家中有三个还是四个孩子，失学的不多。而到上世纪九十年代中叶，一般农村家庭只能送一个孩子上学了，不管考得多好，全家只能保一个最好的上学。到本世纪以来一般农家要想送出一个大学生那就非得集几家人（亲戚朋友）的财力不可，并且每年还听到因孩子考上大学而父母却逼得自杀的事。可见农民增长的收入远不够增长的开支。仅就教育一项农民负担比以前重得何止十倍。同样医疗上，随着社会发展，农民越来越看不起病了。记得上世纪八十年代，一般农民生病住院还较普遍，至少到乡级医院去治治，很少听说在家等病好的，更没有听到等死的。要知在那时若有让老人在家等老，那家的后生就别想在当地抬起头来，当地舆论非得把那家说臭不可。而我这次回家却听说我村及邻村出现过几桩老人在家等死的事，并且奇怪的是村民居然对此给予了理解，或者是变成了麻木。这让我深为吃惊并感到可悲；其四、对政府绝望而麻木。在老百姓眼中政府就是贪官污吏，无休无止的贪钱；政府就是收税收费，没完没了的要钱；政府就是敲诈勒索，防不胜防地榨钱；政府就是连哄带骗，夺走农民手上的一切资源。不公如此，政府自己做了坏事还不算，还跟黑社会勾在一块来欺压老百姓。几十年的教训，让老百姓对政府没有了一丝幻想。我这次回去就没有听到一个说当局好话的，大家甚至连骂娘都懒得骂了，可见绝望与无助。当然农民也还不知道出路何在，还不清楚怎么来改变现实。<br />
　　　　　　<br />
　　　　　　六、满城皆赌、毒<br />
　　　　　　<br />
　　　　　　这次回家触目惊心的还有那满街的麻将桌。我不知道县城从什么时候开始变成了一张大麻将台。反正三年前我回家时没有见到这种状况。这次我从狭长的沿河街走过，见每座楼下的厅中都摆着张麻将桌，并且都座无虚席。在几个十字路口的楼下大厅还聚集着五六张到十来张不等的麻将桌，成群的人分坐在中间，热火朝天地摸麻将。<br />
　　　　　　<br />
　　　　　　为此，我曾奇怪地问在县城做生意的一个朋友： “ 怎么一下县城变成个麻将城了？ ” 他说：县政府将周边许多农村的地连骗带压征过来开发房地产了，村民一下没田种了，也没事干，刚开始一些村民想不开跟政府开发土地过不去，后来政府有意放开他们赌博，一下有一批村民就忙于去赌而顾不上与开发土地作对了，甚至还有批人本不愿被征土地的，一下到赌场居然急着就来卖地，看来引导村民赌博也是化解社会问题的路子。这样一来在政府有意纵容甚至引导下，县城就成了一张大麻将台，全民从赌，形成蔚为壮观的赌博气势。 ”<br />
　　　　　　<br />
　　　　　　县城不仅是公开的全民聚赌，而且在当地还活跃着一支据说有政府与银行为后盾的流动大赌队，附近几县一些官僚与商人多汇聚其中。他们一般每天流动赌资就有几百万，组织者十抽一地收管理费。这么大的流水金额，管理费自然丰厚，没有一些执法部门在背后支持是坚持不下去的。这支流动的赌博大军在当地已活动了一年多了。<br />
　　　　　　<br />
　　　　　　一个没有什么产业特色的县城怎么能支撑得住如此盛行的赌博？这样一来社会治安恶化就是必然的结果。<br />
　　　　　　<br />
　　　　　　不仅如此，这么个县城一年多前居然查出了一个在全国有影响的制毒贩毒案，并且因此还牵连进去一批地方官。那个从福建过来的制冰毒的首犯，居然在全州经营了近十年，在县城下面一个乡的大山中有一个大的冰毒生产基地。可见当地吸毒贩毒的猖獗、盛行。<br />
　　　　　　<br />
　　　　　　面对家乡的状况，实在是怎一个沉重了得！然而，更沉重的是家乡绝不是中国的特例，它仅仅是中国现实的一个缩影。一个没有自身产业特色的小城，表面看起来似乎还是在发展、繁荣，其实是浮躁、虚幻。那里的百姓没有基本的安全保障，整日生活在恐惧之中；那里的百姓没有发展进步的盼望，一切劳动成果远追赶不上各种教育医疗费用增加的负担；村民有限的资源被官僚蓄意剥夺搜括殆尽，官匪勾结让百姓居无宁日；当局恶意诱导与无能管控导致社会沉陷入醉生梦死、赌毒泛滥的境地。<br />
　　　　　　<br />
　　　　　　这是个怎样个绝望的世界啊！我深深为故乡沦陷而悲哀，为华夏这遍土地沉沦而无奈<br />
