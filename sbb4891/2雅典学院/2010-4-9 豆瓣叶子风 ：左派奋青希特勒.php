													<h2>豆瓣叶子风 ：左派奋青希特勒</h2>
						
						
																											<div id="postmessage_556537" class="t_msgfont">左派奋青希特勒，这是我读完《我的奋斗》得出的唯一结论。 <br />
　　 书老早就买下了，当时翻了几页看不进去，就扔在一边。前阵子读汉娜・阿伦特的《极权主义的起源》，对她多次提到纳粹的左翼根源印象深刻，于是翻出这本世界名著来，看能不能从中找到更多的启示。没想到才看到第二章，一阵熟悉的气味扑面而来，“在维也纳的城中，贫富的悬殊，真有天壤之别”！这简直就是朗咸平教授每次演讲的开场白，一针见血，直截了当，立即让我们感受到他坦荡荡对“社会不平等”的愤恨，而这正是我们平时心中不断积攒却又苦苦压抑的情感。他人真好、他洞察现实、他很有良知，我们眼望着讲台不禁发出了赞叹。然后，熟悉朗咸平的人应该知道，该是谈“信托责任”的时候了。果然，希特勒说，“我觉得这种事情只有两种方法可以改进：一是对于社会责任应有深挚的情感，这样才能为我们的发展路径确立一个良好的原则；二是必须痛下决心，除去一切腐败的赘疣。”不过接下来，希特勒就和朗咸平分道扬镳了。因为通常说来，朗咸平会不停重复“信托责任”这个答案，直到所有听众的情绪由激昂转为冷静。但希特勒头脑太混乱了，焦点不集中，东拉西扯，给人的印象是“愤恨太多、责任太少”，似乎他的良心总欠缺了一点深度。这也反过来证明朗咸平不是希特勒，他文化水平高多了，立场坚定多了。 <br />
　　 希特勒挥挥手，告别了良心经济学，继续上路。很快地，他往左拐了一个弯，进了工人运动的机关大院。这里，曾经有一位工团主义的理论家索列尔，是普鲁东、马克思和列宁的大粉丝，他有一位著名门徒叫墨索里尼，是意大利左派的实践家。希特勒大声疾呼道： <br />
　　 “倘若工会目的是改善国家基层里某一阶级的生活状况而且能取得成效，则其行动绝非和祖国为敌，倒是名正言顺的民族运动。” <br />
　　 “假如资方蒙昧无知，误解了正义和公理，那我们民众中的一部分劳工，就可以起而反抗个人的贪婪无理，以捍卫集体的福利，这不仅是权利，更是义务。” <br />
　　 “如果争取待遇和反抗不平等，司法当局未能有适合的解决之道，那么斗争的胜负只能取决于实力强的一方。如果劳工们和大资本家发生了冲突，一开始就没有获胜的希望，劳工们就更应团结一致不畏强权，这是毫无疑问的。” <br />
　　 不过，对于墨索里尼这种社会主义者，希特勒内心是轻蔑的，因为他们沾上了马克思主义的气味。希特勒家境贫寒，但他发愤图强，勤思考、爱艺术，很早就成了理想主义青年。他也很早就能把“爱国主义”和“民族主义”分开，像牛博网网友一样反对前者，又像强国论坛网友般支持后者。他说，“十五岁那年，我便能把效忠君王的‘爱国主义’和人民立场的‘民族主义’分析得很明白。我始终爱好民族主义的观念。”本来他情感上不憎恨犹太人，后来是理智说服了他。欧洲最流行的说法是犹太资本家控制了经济命脉，他一直将信将疑，但随后他却发现犹太人操纵了新闻、艺术、文学和戏剧，这对于怀才不遇的这个文艺青年来说，才是真正的震惊和刺痛。接着他在政治活动中，又不可思议地发现犹太人在操纵思想和舆论，霸占了平民和劳工运动的领导权。于是，一个令人热血沸腾的念头涌现了：犹太人有惊天大阴谋，商业霸权、货币战争、文化侵略。怎么办？我们要狼图腾！日耳曼人要说不！抵制抵制抵制！希特勒痛恨马克思主义，原因是它由犹太人创立，是彻头彻尾的“犹太学说”，却成了左翼战线上的主流。他认为，一定要揭穿这个犹太阴谋，让人们明白“马克思主义滥用了社会主义的美妙文字以欺世”。在他看来，马克思主义以阶级斗争破坏日尔曼民族的团结，以“全球化”（当时叫“国际主义”）的口号劫掠德国的利益，实质是为犹太人的全球霸权服务。希特勒满脑子左翼思想，他坚信未来的世界必是社会主义，所以他思索的不是姓资姓社的问题，而是那个美妙的大同世界将由谁来统治。既然马克思主义是犹太人的社会主义，那么要与这个劲敌对抗，就必须建立强大的日耳曼人的社会主义。希特勒为此发出严正警告：“要是犹太人靠了马克思教义的力量，战胜了世界各民族，那么这皇冠便成为人类送葬的花圈了，地球又将空无人类而运行于太空之中，和数百万年前一样。” <br />
　　 互相憎恨的人总是相似的。希特勒将马克思主义者树为头等大敌，紧紧尾随他们身后，观察他们的一举一动，并从他们身上学习各种先进经验。“据我看来马克思社会主义党他们最擅长而又能充分运用的工具，就是宣传，我立刻觉悟到如果把宣传运用得法，那确也是一种正当的技术，这种技术，实在是那班中产阶级各党所不知道的，只有基督教社会主义运动曾把这种工具当作教育的艺术来运用，而且他们大部分的成功，实在是得力于此的。”“马克思主义之所以能对群众有惊人的蛊惑力，并不是得力于犹太知识分子的著作，而是得力于历来许多滔滔不绝的支配群众的口头宣传。”“马克思主义的报纸，执笔的人大都是煽动家。”“过去马克思主义获得成功是因为他们运用政治和暴力的决心”。对于对手发动群众的才能，希特勒更是毫不吝啬地给予赞美：““在大战以后，我曾参加过柏林皇宫前的马克斯主义的民众示威运动。一幅幅的红旗，一条条的红领巾，以及一朵朵的红花，在空气中自由飘荡，显示了十二万参加大会的群众的声势。我深切地知道这种伟大而庄严的气象，确有感动局外人的一种魔力。”希特勒处处模仿对手，以对方为自己的标尺。“我雇了两辆货车涂满红色，上面插了党旗；每一辆车上载着党员十五至二十人，满街驰骋散发传单，作为晚上举行群众大会的宣传。这是创举，除了马克思主义的共产党外，再没别的党曾经在汽车上插旗游街宣传。”“当时的慕尼黑，除马克思主义的共产党外，不再有其他的党能够像我党举行大规模示威运动。”在招募党员方面，希特勒也是专挖对手的墙角，他认准了大家的支持人都是同一类人。希特勒自豪地记录了自己这一计谋的成功： <br />
　　 <br />
　　 我们集会里常有反对派(共产党)来参加，他们常常结队而至，偶然还有少数的煽动家混入其中，他们每个人的脸上，都呈现出与我们决一雌雄的表情。 <br />
　　 共产党员来我们这里捣乱，这是有原因的。我们经过周密考虑，决定散发大量传单和标语，故意来激怒他们，使他们愤不可遏地参加我们的集会一我们的用意，就是为了离间他们，趁此机会来向他们宣传。 <br />
　　　 我们的敌人，让“有阶级意识的”工人成群结队参加我党的大会，想用无产阶级的力量，摧毁我们“专制又反动的计划”。开会前的45分钟内，会场上已经充满了工人。他们好像是火药桶，大有一触着导火线立刻爆发的趋势，可是往往事与愿违，他们到会时是敌人，到了散会，他们虽然未必和我们握手，但至少他们也能反躬自省，要去反思自己主张的主义是否正确。 <br />
　　　　后来，他们的党这样地说：“无产阶级们呀!你们千万不要去参加民族主义煽动家的集会!”同时，共产党的报纸也这样呼吁。 <br />
　　 民众们见到了共产党的报纸对我们注意了，因此对我党也就加以注意了。 <br />
　　 <br />
　　 希特勒坚持自己才是真左派，真社会主义者，真为弱势群体代言。所以他要把马克思主义的话筒抢过来，用自己的嘴说出社会主义的真理，喊出代表人民心声的口号，这样才能踢走“伪社会主义”，赢得劳工阶层的支持。他像左大培般痛斥官僚阶层的卖国和自私：“为了要顾及他们的愿望，就不妨去牺牲了劳动阶级的利益，来充实那强盗的钱袋；我们如果不要德国灭亡，那就不能坐视着他们的为所欲为。”他像韩德强般强调资本国有化有利于劳工：“资本纯粹是劳动的产物，它和劳动一起成为了左右人类活动的基础因素。这就是资本对于民族的意义”，“国家对于资本的责任是十分简单明了的。国家只要注意资本供国家使用，这样便就可以定下两个目标：一方面维持着有效率的独立的国民经济，另一方面是维持工人的社会权利。”他像甘阳般反对资本主义的全球化：“我对德意志发展的途径认识得十分清楚，所以我觉得他们最坚苦的战斗，不在对付敌国而在对付国际资本主义”“我们不要再被我国资产阶级的狡黠政客所嘲弄了......他们的目的，是要准备一切，使金融业和交易所的真正国际资本能够来统制德国。”他像韩少功般忧患商业腐蚀：“现在已经使贫富悬殊更明显了。富者和贫者比邻而居，势必酿成悲惨的后果。人民既开始受着贫穷和大量失业的痛苦，所以接踵而至者，便是不平和怨恨。民族经济破坏而所生的不良现象，还有比这更坏的。因为商业宰制了全国，所以金钱也就变成了万能了，举国上下，个个人都成了拜金主义的奴隶，风气败坏到了这等地步。”他还像孔庆东般揭露资产阶级的无能和虚伪：“如果这一时代的人，他明知自已有着某种缺陷，而竟伪称没有补救的方法而苟且图安，那么，这种社会，断无幸存之可能。我国的小资产阶级，现在便是这样的。我们不能再被他们欺骗了。我国的资产阶级人士的腐败现象已经达于极点，他们决不能担任任何文化事业的。我以为他们的腐败，并不是由于他们的道德堕落，而是由于他们的懒惰习性，积重难返。”深揭狠批所有恶势力后，他又像一名“乌有之乡”的忠实信徒开始谈希望，谈理想，谈国家干预分配，谈共建和谐社会： <br />
　　 <br />
　　 “我党的任务之一在于揭示一个新时代，期望到了那个时候，人人能各取所需；并能坚持一个原则，反对人生唯一的目的是物质享受。 <br />
　　 要实现这件事，国家对于工作的报酬，必须有精细的规定并建立起分配的标准，使得诚实的工人能享受有秩序和光明正大的公民生活。 <br />
　　　　希望大家不要以为这是空虚的幻想，不要以为现在世界上还不容许且不可能能实现这种理想。我们虽然愚蠢，还不致于去妄想乌托邦马上降临这个世界。但我们也决不可因此自暴自弃，不去努力根治缺陷，铲除弱点，追求最后的理想。 <br />
　　　　这种理想要实现，途中必有种种障碍，这是势所难免的。所以我们更要尽自己的心力，共赴最后的目的。” <br />
　　 <br />
　　 相似的人连憎恨的方式都是相似的。毛主席清洗不同路线左派的方式是：把他们打成右派，归为走资派。布尔什维克对付纳粹如出一辙，斯大林咒骂它代表大资本家利益，托洛茨基贬低它为小资产阶级政党。反过来，希特勒对付马克思主义者同样如此，他把他们定义为“左奸”，名左实右的走资派。而完成这一论证，他借助的是一种阴谋论，“犹太人”成了马克思主义通往资本家势力的地下通道。希特勒说，马克思主义只是在利用社会主义：“在剪除社会罪恶的斗争上，重要的在于世界观，于是犹太人创立了马克思的学说。他把社会上一切公正合理的要求和他的学说相混合而成一片，以求他的学说获得流行。”但其终极目的是为犹太富人服务：“马克思主义固然有着他的目的，而且还抱有一种建设的雄心，虽然他们所建设的不过是犹太人对世界金融的垄断。”马克思主义的工运鼓吹国际主义，和大资本家全球化的战略一致的：“工会制度的主要目的，并不是阶级斗争，可是马克思主义硬要叫它成为阶级斗争的工具。马克思主义首先创设这种经济的武器。而国际主义的犹太人，就来用破坏自由独立的国的经济基础，毁灭这种民族的工商业，而经营他们自己的工商业。他们的目的，是要使民族去做那不分国界的犹太人所操纵的金融界的奴隶。” <br />
　　 希特勒的这一手法是不是很眼熟？后来存在主义流行时，雷蒙・阿隆在萨特身上发现了同样的东西：“美国人在萨特的魔鬼学中的地位，如同犹太人在希特勒的魔鬼学中的地位一样。”实际上，今天的知识分子圈里，这种魔鬼学还在流行，只不过他们对资本家的仇视不是通过“犹太人”，而是通过“美国人”、“白种人”或“西方人”。但回头再看，作为一代左派宗师，希特勒确实建树良多，为各种先进代表思想开了风气之先。他像司马平邦般嘲笑“普世价值”的自相矛盾：“如果我们并不准备去探讨马克思理论的实施结果，那么，既希望以种族去估量个人价值，又去攻击马克思派的‘人人都是一样，没种族的分别’的理论，那真是傻子。”他像司马南般指出“普世价值”只是牟取一方利益的幌子：“国际性的马克思主义，马克思称之为一种普遍的世界观，久而久之便形成了政治上的信条......马克思之所以如此，那是为了谋犹太种族的利益而已。”他像梅宁华般抨击“造谣的言论自由”：“我们不必去提起那惯于造谣的马克思派的报纸，因为他们把造谣认为是一件事，好像猫不能不去捕鼠一样。他们唯一的目的，便是摧毁国家和人民的抵抗力，使他们成为国际资本及其主人――就是犹太人――的奴隶。这种毒害民族的行为，国家对它用过严刑峻法吗?绝对没有：只有几次温和的警告，以及实在说不上重的罚令而已。”他像黄万盛般歌颂不是原罪的革命：“法国革命，因为能够产生出一种伟大的新观念，因此他们就发现了成功的秘决；俄国的革命也是这样的，法西斯主义是完全由理想去引发力量，他们的理想是整个民族的复兴，结果是为他们的民族造福不浅。” <br />
　　 <br />
　　 这时的希特勒三十多岁，还在为自己的理想付出代价，他是在狱中艰难地完成了这本著作。人们经常谴责他当上元首后的霸道蛮横，而总是忘记了他曾是一介贫寒的老百姓，一个钻研过马克思主义的青年，一个为工人抱打不平的政论家，一个为民族利益牵肠挂肚的思想家。实际上，他在上台之后，依然怀抱理想，尽力兑现自己对低下层人民的诺言。他主张“国家监督和领导国民经济”，他没收私人工厂将其国有化，打击以宋鸿兵所痛恨的罗斯柴尔德家族为代表的财阀，大幅提高劳工阶层的薪酬和福利，孜孜追求“让德国每一户人家的餐桌上有牛排与面包”的小康社会。希特勒被人诟病的是他的国家主义和民族主义，这在欧洲的政治光谱里，划归“右派”。但去看看那些鼓吹国际主义的共产党人，哪一个在掌权之后不是坚持国家主义和民族主义的呢？如果“国际主义”属于左派，希特勒发动侵略战争又何尝不是一种“国际主义”的实践。其实，早在《我的奋斗》中，希特勒就已经承认自己和共产党人有非常雷同的梦想：“民族主义世界的理论，应该锻造成一副工具，以作武力的防御――恰好是像马克思列宁主义党的趋于国际主义一样。” <br />
　　 当然，还是必须看到，希特勒与最成功的左派仍有一个巨大的区别，那就是他“种族仇恨”以及相关的“种族屠杀”。虽然纳粹党杀人集中而残暴，但在受害者数量上，远远比不上那些红色政权基于“阶级仇恨”带来的“阶级屠杀”。而且，希特勒死于非命，不像斯大林及其一众弟子们能够安享晚年。从这个角度看，希特勒固然斗志可嘉，却是不够纯粹、不够彻底的左派。</div>

							
							
							
							
