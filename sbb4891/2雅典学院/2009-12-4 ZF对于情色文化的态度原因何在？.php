																											<div id="postmessage_357416" class="t_msgfont">再次推荐。这篇文章是我为译言翻译的，还在进行中，作者从心理学角度分析中国文革时期的洗脑。下面的是其中“对纯洁的要求”的小节。等我最终完成译文，会把这篇文章先贴在小组里。<br />
<br />
<a href="http://www.rickross.com/reference/brainwashing/brainwashing19.html" target="_blank">http://www.rickross.com/referenc ... brainwashing19.html</a><br />
<br />
北卡罗莱纳大学出版社 / Chapel Hill and London<br />
作者：Robert Jay Lifton，医学博士<br />
<br />
下文节选自Robert Jay Lifton的《思想改造与极权主义下的心理：对中国的‘洗脑’进行的研究》一书的第22章<br />
<br />
对纯洁的要求<br />
<br />
在对思想改造的环境中，就像在基于意识形态的极权主义中的所有情形下一样，经验得来的世界被一下子分割成为纯洁的和不纯洁的，分成绝对的高尚和绝对的邪恶。纯洁和高尚的理所当然地是那些与极权意识形态和政策相符的理念，感觉，以及行为；任何其他的都很自然地被视作坏的和不纯洁的。人没有任何东西可以躲过严苛的道德审判的洪流。一切造成不纯洁的现状的“污垢”和“毒素”都必须被搜出来并被消除。<br />
<br />
在这种要求下的哲学假设是，绝对的纯洁是可以达到的，以这种纯洁的名义对任何人所做的任何事最终是道德的。可是在实际操作中，没人真的被指望能达到如此这般的完美。这个矛盾也没法当作一种建立一个所有人都励志实现的高标准的手段而被打发掉。思想改造见证了它更恶毒的后果：通过定义并且操纵构成纯洁的条件，然后展开一场针对不纯洁的全面战争，基于意识形态的极权主义构建了一个内疚和羞愧的狭窄世界。这进而被一种持续变革的道德观所持续下来，要求人为了某种不仅不存在，而且实际上与人的生存状况背道而驰的东西永远而痛苦地努力奋斗。<br />
<br />
At the level of the relationship between individual and environment, the demand for purity creates what we may term a guilty milieu and a shaming milieu. Since each man's impurities are deemed sinful and potentially harmful to himself and to others, he is, so to speak, expected to expect punishment - which results in a relationship of guilt and his environment. Similarly, when he fails to meet the prevailing standards in casting out such impurities, he is expected to expect humiliation and ostracism - thus establishing a relationship of shame with his milieu. Moreover, the sense of guilt and the sense of shame become highly-valued: they are preferred forms of communication, objects of public competition, and the basis for eventual bonds between the individual and his totalist accusers. One may attempt to simulate them for a while, but the subterfuge is likely to be detected, and it is safer to experience them genuinely.<br />
<br />
People vary greatly in their susceptibilities to guilt and shame, depending upon patterns developed early in life. But since guilt and shame are basic to human existence, this variation can be no more than a matter of degree. Each person is made vulnerable through his profound inner sensitivities to his own limitations and to his unfulfilled potential; in other words, each is made vulnerable through his existential guilt. Since ideological totalists become the ultimate judges of good and evil within their world, they are able to use these universal tendencies toward guilt and shame as emotional levers for their controlling and manipulative influences. They become the arbiters of existential guilt, authorities without limit in dealing with others' limitations. And their power is nowhere more evident than in their capacity to &quot;forgive.&quot;<br />
<br />
<br />
在个人和环境的关系层面上，对于纯洁的要求造就了我们可以称之为内疚社会和羞愧社会的一种环境。既然每个人的不纯洁都被视为有罪的并对他自己和他人有潜在危害的，那么，这么说吧，他理应期待得到惩罚 - 这导致了一种内疚和他的环境之间的关系。同样地，当他在驱除自身的不纯洁这件事上面没有能够达到主流的标准时，他理应期待得到羞辱和排斥 - 这就建立了一种羞愧和他的环境之间的关系。更进一步，这种内疚和羞愧的感觉变得具有很高价值：它们变成大家所喜好的沟通方式，公众进行竞赛的目标，成为个人和他的极权的指责者之间的最终纽带的基础。一个人可能试图冒充内疚和羞愧于一时，但这种招数很可能被识破，所以还是真诚地感受它们比较安全些。<br />
<br />
不同的人感受内疚和羞愧的难易程度差异很大，这取决于在生活早期发展出来的模式。但是由于内疚和羞愧是人类生活的两种基本情感，这种差异不过是程度问题而已。每个人都由于对于他自己的局限性和未满足的潜能的深远的内在洞察而为内疚和羞愧打开了大门；换句话说，每个人都由于他的存在性内疚而变得脆弱。既然基于意识形态的极权主义者变成了在他们的世界里对于高尚和邪恶的最终裁决者，他们就可以利用这些在人们中间普遍存在的感到内疚和羞愧的倾向作为情感杠杆来控制和操纵影响。他们变成那些存在性内疚的仲裁者，对付别人的弱点的不受任何限制的权威。没有什么比起他们可以进行“宽恕”的能力更能够体现他们的权力。<br />
<br />
The individual thus comes to apply the same totalist polarization of good and evil to his judgments of his own character: he tends to imbue certain aspects of himself with excessive virtue, and condemn even more excessively other personal qualities - all according to their ideological standing. He must also look upon his impurities as originating from outside influences - that is, from the ever-threatening world beyond the closed, totalist ken. Therefore, one of his best way to relieve himself of some of his burden of guilt is to denounce, continuously and hostilely, these same outside influences. The more guilty he feels, the greater his hatred, and the more threatening they seem. In this manner, the universal psychological tendency toward &quot;projection&quot; is nourished and institutionalized, leading to mass hatreds, purges of heretics, and to political and religious holy wars. Moreover, once an individual person has experienced the totalist polarization of good and evil, he has great difficulty in regaining a more balanced inner sensitivity to the complexities of human morality. For these is no emotional bondage greater than that of the man whose entire guilt potential - neurotic and existential - has become the property of ideological totalists.<br />
<br />
个人于是变得用同样的极权主义者的高尚和邪恶这两极来对他自己的品格做自己的评判：他倾向于给他自己的某些方面染上过分的美德的光辉，而又更加过分地谴责其他个人品行 - 全都根据他们的意识形态标准。他还必须将他的不纯洁看作来自外界的影响 - 就是说，超出他所熟悉的这个封闭的极权主义世界之外的一直充满威胁的世界。于是，一种减轻他所背负的内疚重担的最好的方式之一就是去谴责，连续不断地和充满敌意地谴责那些外界影响。他越是内疚，就越是仇恨，而那外部世界就看起来越是充满威胁。以这种方式，人类普遍的“投射”心理倾向被滋养和制度化，导致群体性的仇恨，清洗异端，以及政治或者宗教性的圣战。更进一步，一旦个人经历了极权主义者的两极分化的高尚和邪恶，他会很难重新获得对于复杂的人类道德的更平衡的内在感受。因为没有任何情感枷锁可以比得上当一个人的全部内疚能力 - 神经上的和存在性的 - 都成为了意识形态极权者的财产时更沉重。<br />
