&nbsp; &nbsp;中国娱乐圈甚至中国社会的主流观念就是这样开始被扭曲的。这扭曲，早已不是什么新鲜事了，只不过现在的扭曲趋势越来越明显，逐步进入大众的视野中罢了。笔者认为，早在木子美，芙蓉姐姐出没的那个时期，就出现了这样的主流大众娱乐观的扭曲。那是玩的是“下半身”与“出位”，那个时候情况远没有现在糟糕，观念扭曲的范围还仅仅是在一个比较小的圈子里；但毋庸置疑的是，以变态为常态，以丑为美，为出名而暴露的萌芽就是在那个时期出现的。<br />
