													<h2>浅谈马基雅维利主义权术观</h2>
						
						
																											<div id="postmessage_718280" class="t_msgfont">浅谈马基雅维利主义权术观<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; ――读《君主论》有感<br />
&nbsp; &nbsp;在西方政治思想史上，没有谁像马基雅维利那样备受争议，也没有谁像马基雅维利那样背负恶名。诸如“恶魔”，强权政治的维护者，失意的官场政客，共和主义的理论家，伪科学主义者等。列奥施特劳斯也称他为罪恶的导师。希特勒成《君主论》是他的床头读物。正是他学说的非道德主义色彩，加上独裁者不加掩饰的推举，让马基雅维利声名狼藉。但是真正的马基雅维利是什么样的呢，人们又是怎么看待他在政治思想史上的贡献呢？本文将从《君主论》的一些观点来管窥马基雅维利的政治思想，粗浅探讨一下马基雅维利的权术观。<br />
关键词：马基雅维利&nbsp; &nbsp;君主论 权术<br />
&nbsp; &nbsp;四百多年来，在世人心中，“马基雅维利式的”这个形容词，一直是残酷，邪恶，阴险等直字眼的代名词。而马基雅维利本人也被描述为喜欢玩弄权术，伪善，为了全力不顾道德，不守原则的下流政客。世人皆是马基雅维利的政治哲学就是为达目的，不择手段。《君主论》是马基雅维利的著作，马基雅维利的罪名大部分是因为该书的影响。那么该书到底阐述了什么样的权术思想让马基雅维利如此“受伤”，本文将从以下几个方面浅谈马基雅维利的权术观。<br />
一， 为达到目的不择手段。<br />
&nbsp; &nbsp; 马基雅维利生活的时代正直意大利遭受法国入侵，国破家亡的危急关头。面对国家的分裂，人民生活的困苦，他意识到国家需要一个强有力的君主来挽救意大利，完成国家的统一和独立。<br />
在《君主论》中马基雅维利认为，在政治游戏中无所谓善恶对错，有的只是阴谋，有的只是野心，征服者和被征服者，而历史一向是胜利者来决定的。人民有屈从权力的天性，君主需要的是权力，统治。而不是关爱。为了说明这点它从人性本恶的人性论出发来论证君主不择手段夺取权力的合理性。他认为君主要有狮子一样残忍，狐狸一般狡诈。要时刻以强权来维护统治，“慈悲心是危险的，人类爱足以灭国”。<br />
“只要目的正确，可以不择手段” 是马基雅维利的名言。因此在《君主论》问世以来，许多学者相信，马基雅维利思想是完全脱离伦理道德的权术论。这样一种价值取向撕破人们道貌岸然的面具，向我们揭示人类内心中最肮脏，最卑鄙，最奸诈的成分。<br />
虽然马基雅维里认为君主可以为了目的，不择手段，但是他建议君主在表面上一定是个爱民如子，仁慈宽厚，在道德上是个处于高位的人，他能够让他的子民相信他是个“集美德于一身的人”。肮脏下流的事情自己是不需要出头的，要让别人去干，这样即使事情败露也不需要自己去承担骂名，让君主的颜面扫地。关键时刻可以为了自己的目的去找替罪羊。打击报复也要彻底，以免留下祸患。同时马基雅维利认为君主为了保证自己权力的自主性，不要相信任何人，不要让自己的大臣了解自己的本性，要给大臣有神秘莫测的感觉。自己也不要依赖别人。“最危险莫过于意气相投的人”。所以要怀疑一切人。总之为了保住君主的地位，任何手段都是可以的。<br />
二， 善行和恶行一样容易招人怨恨<br />
&nbsp; &nbsp; “一个君主如果要保持自己的地位，就必须做到怎样做不好的事情，并且必须知道视情况的需要与否使用者一手或者不使用这一手”。在保持君主地位政治斗争中实力决定胜败，而实力表现为武力，金钱，谋略。<br />
&nbsp; &nbsp;&nbsp;&nbsp;就武力方面，马基雅维利从人性本恶方面出发，认为人类都是拥有普遍的利己主义，在利益面前时没有任何道德可言的。人类都是希望逃避灾难，追逐权力和各种利益，不希望承担任何责任。他们为了利益是可以被判君主的。而且他认为背叛自己尊敬的人比背叛自己有所畏惧的人容易。而且对人施恩，当这种恩远远超过他们说能报偿的能力，感恩就可能转化成仇恨。但是武力也不时任意使用的，而是由一定的限度。他因该和君主统治一样是一门艺术。武力要和施恩相结合，这样才会产生敬畏结合的效果。武力镇压应当快速的实施结束，臣民较少的感觉到痛苦，但也能感觉到畏惧，恩惠要一点一点地给与，这样才能使臣民感觉到君主的恩惠。<br />
&nbsp; &nbsp;&nbsp;&nbsp;马基雅维利认为金钱是武力的后盾。所以君主要厉行节俭，只有国库充实，你才能保证国内安定，才有精力财力去对付政敌和对外战争。人民是最痛恨增加赋税的。所以你必须为未来未雨绸缪。但对于那些出身卑微的人，因为他们没有武力和金钱，只有用欺诈等权谋来获取资本。欺诈和伪装在马基雅维利纳利那里是君主不可或缺的统治策略。当公开的对抗不能获胜的时候，就要大胆的搞谋略，以达到自己的目的。君主眼善于伪装，让你的臣民和对手不知道你的真实想法，这样你才能在他们面前具有权威性，让他们产生畏惧感。君主“为了保住你的地位，当你认为自己需要的那些人――无论是民众也好，军队也好，贵族也好――腐化堕落的时候，你为着实他们高兴，迎合桌他们的脾胃”。<br />
三， 只要有合适的理由，君主可以不遵守诺言。<br />
马基雅维利在君主论中说，君主在面对他的臣民时，应不应该守信用，遵守诺言不是事情的关键，关键问题是遵不遵守诺言对自己有何利弊。如果讲真话，遵守诺言对自己有利那就讲。反之君主就可以不讲信用，不遵守诺言。君主不单要善于欺骗，而且还要善于伪装，为自己的行为粉饰遮掩。“要做一个伟大的伪装者和假好人”。<br />
他给出欺骗的两大理由。一就是大多数臣民是头脑简单的人，易于被欺骗利用。二是君主与臣民的距离是臣民不能了解君主，不知道君主的真面目，易于欺骗。君主完全可以利用这两点来进行统治。再加上马基雅维利提出的人性本伪，人性本恶，也为君主的欺骗政策的必要性提供依据。同时他指出君主失去君主地位的理由。一是因为君主人以夺取臣民的财产，二是君主抢占别人的妻女。这两种行为都会导致臣民的反抗。为了避免君主的为的丧失，君主必须对臣民财富妻女存贪婪之心。否则“招人民之憎恨，虽金城汤池，也不足以保障王者的安全。”同时他指处为了不减少臣民的指责，树立君主形象，君主在奖赏施恩是一定要亲临，而对于刑法厮杀之事就让别人去做好了。<br />
四， 谁是促使他人强大的原因，谁就自取灭亡<br />
&nbsp; &nbsp;&nbsp;&nbsp;在《君主论》中马基雅维利说：“谁是促使他人强大的原因，谁就自取灭亡”，意思是说面对威胁自己统治的人和势力，君主一定不能心慈手软，要果断铲除异己，是自己的统治处于绝对安全的状态之下。谁姑且养奸，谁就会灭亡。<br />
虽然以后的政治学对这样的行为逻辑大加指责，但是历史一次次验证其正确性。在祖国历史上的宋朝，开国皇帝赵匡胤在建立宋朝以后，为了防止大臣居功威胁自己的统治，以巧妙的谋略解除石守信你等大臣的兵权，稳固了大宋统治。<br />
而另一个相反的例子这出现在唐朝唐玄宗身上。作为大唐王朝的君主，在发现安禄山地方实力日益增大，威胁其中央统治安全时没有采取果断措施，姑息养奸，结果差点皇位不保。<br />
此外，马基雅维利的性恶论认为人天生是邪恶的，人们是“忘记父亲之死比忘记遗产的丧失还来的快些”。君主论的许多权术思想都是基于人性恶提出来的。君主必须比别人更邪恶凶残，才能保住自己的地位。<br />
五， 小节<br />
马基雅维利的权术思想为以后的统治者提供了一种准则和策略。在他的《君主论》中，处处体现着这种权术思想。但是君主论不只是写给君主的，他是一部教育年轻人的思想利器。也是一部反封建的思想武器。作为一部政治学名作，对世界以后的政治思想产生了深远的影响。虽然不同的人对君主论又不同的评价，褒贬各异，但是有一点是不容置疑的，那就是君主论中的权术思想在现实的政治生活中被实践，验证。这其中的积极意义也是不能掩盖的。<br />
