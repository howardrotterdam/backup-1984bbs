香港明报, 2009-4-18<br />
<br />
首先，我要感谢香港大学的同学们，你们的公投以压倒性的比例通过了要求平反六四的诉求，为港大的学生洗刷了名誉，也再次证明追求民主是人类的基本理念。感谢你们坚持了做人的基本原则。<br />
<br />
同时，看到港大有人批评柴玲是逃跑的学生领袖，我也深深地感到，20年过去了，即使在香港，很多人对当年发生的事情也不是很清楚了。这是非常令人遗憾的。作为当年的参与者，我觉得我有责任就一些基本问题做一些澄清，尤其是关於柴玲的问题。<br />
<br />
关於柴玲，外界一直有很多不公正的评价。最近香港大学学生会会长说柴玲带头逃跑就是一例。更多的说法，是关於《天安门》纪录片中她的一段话，大意是说：「我们走，让别人流血」之类。多年以来，每一次演讲或者公开发言，只要涉及到这个问题，我都一再强调，外界对於柴玲过分不公正。20周年之际，我必须再次站出来为她辩护。<br />
<br />
首先，分析一个人不能仅仅根据一句话，这是基本常识。柴玲以一个女孩子，担任天安门广场指挥部总指挥，可以说是尽心尽力地希望推动中国民主化进程。在整个八九民运的过程中，柴玲做了大量的工作，怎麽能因为一句话就抹煞掉她的全部呢？<br />
<br />
她确是最後一批撤出广场的<br />
<br />
其次，就算柴玲说了一番「我要活著」之类的话，但是第一，那样的话我们应当放在前後的内容中来看，不能断章取义，柴玲接受记者采访的时间长达一两个小时，怎麽能够仅仅根据她这两句话对她，甚至对学生领袖群体下结论呢？第二，身为广场总指挥，柴玲面临的压力之大，是任何外人无法比拟的，何r得知自己的同学被屠杀之後，本来就感性的她，激愤之下说一些极端的话不仅是可以理解的，也是应当被谅解的，外界不管具体情景就下判断，对柴玲极为不公平。<br />
<br />
第三，也是最重要的是，很多人都批评柴玲「让别人流血，我要活著」这样的话，但是他们都忘记了一个基本的事实，那就是，不管柴玲说了什麽，但是实际行动上，她确实是最後一批撤退出广场的，这一点有录影和在场的成千上万的同学可以证明。我们判断一个人，到底是看她的言论呢，还是看她的行动，这不是一个很简单的标准吗？为什麽那些指摘她的人不提她最後撤出的事实呢？<br />
<br />
最後我要说的是，最近一些年来，柴玲比较淡出民运，有些人对此也颇有微词。但是大家想想，长期以来，为了参加民主运动被迫逃亡，有家不能回的柴玲，曾经领导了整个天安门广场的学生行动的柴玲，多年以来却承受了外界这麽多的不公正的评价，很多还是极为恶意的攻击和羞辱人格的辱侮，她的内心会是多麽的难过？我看了很多对她的评论，内心极为不忍：那些人对开枪杀人的刽子手都没有像对柴玲那样苛刻，这个世界还有公理可言吗？！我们没有必要批评她退出，如果是我面对这样的不公，我也不一定能够坚持下去。<br />
<br />
我不是说柴玲所作的一切都是正确的，但是我认为我们对於柴玲，应当更加公正一些。为柴玲辩护，并非因为我们曾经是战友，事实上，我们已经有四五年没有联络了。为柴玲辩护仅仅是因为作为一个历史研究者，我不希望历史对任何一个人是不公正的。</div>

							
							
							
							
