													<h2>为什么要反基督教</h2>
						
						
																											<div id="postmessage_368759" class="t_msgfont">一种宗教，作为个人信仰，本来无可厚非。但是，对于某些信徒或信仰，你可以不反他，他却要跳出来找麻烦。<br />
有这么多人质疑进化论，所用的都是基督徒的那一套逻辑。实际上，现在这个世界上就没有哪种主要势力有兴趣和进化论过不去，除了基督教以外。<br />
至于反对儒家的人，倒有一部分是继承了无产阶级文化大革命的光荣传统，也就是说，他们是反对一切传统的疯子。但大部分人还是“反对一种不好的传统”，那么，从时间上同样有资格与儒家并称为传统的”好的传统“是什么呢？（没有好的就无所谓不好的。）在我们的兴趣范围内，显然也只有基督教文化的传统。<br />
基督徒和中基督教毒很深的人。他们是不会闲着的，一定要把基督教的反智主义发扬光大，用吹毛求疵的方法来质疑别的思想体系。这种情况下，如果放着基督教不反，就难免让人们产生一种基督教有多么伟光正的错觉，并使他们的谬论”理直气壮“起来。而实际上，基督教才是最荒谬最独断的。<br />
本文不想再具体讨论进化论和基督教本身。在前面的辩论中，质疑进化论的人几乎无一是有生物专业知识的，这种人却非要用进化论中的细节之争来作为论据（其实他们根本就不懂）。而为基督教辩护的人，很多也不懂宗教，比方说：不读《圣经》尤其不读《旧约圣经》；不知道基督教与犹太教，基督徒与犹太人之间的”继承与仇恨并存“的具体情况。<br />
对这类人，是没办法通过辩论来说服的：他们既不懂，又不打算学。他们需要去上生物课或者神学课才能补齐所需要的知识，不然就只能一直傻下去。</div>

							
							
							
							
