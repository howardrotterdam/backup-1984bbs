																											<div id="postmessage_528170" class="t_msgfont">读“用革命结束暴政”的感想 <br />
<br />
任道兴&nbsp;&nbsp;<br />
<br />
曹长青先生在「用革命结束不暴政」一文中，谈到辛亥革命的令人扼腕的结局时说；“问题是在革命之后，国共两党都没有走美式的以保护个人权利为核心价值的建国之路。……在思想理论远没有到位的情况下，中国的革命最终走向独裁专制是必然的。”<br />
<br />
这里提出了一个耐人寻味的问题；同样是用革命推翻暴政，为什么美国走的是一条康庄之路？中国却走的是一条污浊之路？为什么美国的开国先贤一开始就强调尊重个人的权利价值？而中国百年来的政治强人从来就对个人、个体的概念毫无所识？中国人天生就有一种“个体的软骨症”？中国人天生就有一种群体的“被”的奴性？所谓博大精深的文化血液里，天生就没有“个体”的基因？<br />
<br />
我想这才是问题的焦点和要害之所在，这才是值得反思的结症之所在。如果这个问题不清不解决的话，革命的成功又有何意义？改良的成功又有何意义？无论那一种方式的成功，谁又能保^不会是另一轮的“你方唱罢我登场”？<br />
<br />
曹先生在论述用暴力推翻暴政时说；“今天，在中国民运组织，异议人士中没有任何人主张结束中共专制之后，要建立另一个独裁制度。”是的，民主已是海内外中国人的一致共识。但是，共识和实际的操作不是一回事。中共建政之始和诸民主党也达成过共识，孙中山退位之初和袁世凯也达成过共识，然而最后都车落陷井，不能自拔。可见，共识和具体的实践并不相等。<br />
<br />
再者，我们也可以作这样一个不切实际的设想；美国是个实行了二百多年民主政体的国家，可谓金刚铁骨，但如果让他们现任的总统，国会议员全都下马，全换上海外的民运人士，或是中国大陆的异议人士，让他们来掌控现成的美国政治。那么，他们就真的能执掌操办得了吗? 他们就真的能用他们的民主理念和牺牲精神来润滑和延续美国的民主政治吗？<br />
<br />
我看未必。－－如果上述问题没有清没有解决的话。更何况，一旦中国实行政党轮替，百废待举，鳞伤遍体，而民运人士、异议人士也必然是新政体的主政者主导者，－－如果上述问题没清没有解决的话，你能保^新的民主政体不再陷入泥淖？不再发生逆转吗？一百多年前的辛亥革命已经有了前辙，而目前，海外的民运，种种的民主实践的操作中，似乎也可算是个后辙。此其后辙，我们真的应该有如临深渊的战兢，有当头棒喝的惊醒。<br />
<br />
痛恨中共，或受到中共的残酷迫害，与搞民运不是一回事。即使你是民运的启蒙者，你对民主的理论纲领有精道的造诣阐述，但这与脚踏实地的实践，身体力行的履行，同样不是一回事。民主是自然的，也是高尚的。对于一切有志于民主事业的人来说，除了要解决上述的认知和身体力行的实践外，其人格上的品质高尚，胸怀上的海纳百川同样是重要的。<br />
