																											<div id="postmessage_529463" class="t_msgfont">包括查理一世在内的英国革命中的人头远远少于法国革命。这是妥协的一种表现，贵族及平民没有风暴式地清洗他们原来的统治者。以鲜血和人头的出现证明一切革命没有量的差异，丧失了讨论的基础。<br />
<br />
革命（甚至包括改良），没有人头落地，是奇迹，或者说是神迹。<br />
<br />
我们期待的是尽量少的动荡，尽量少的鲜血。这些就是少的学费。<br />
<br />
但是更重要的是，当我们交了学费，是不是朝更加文明的方向进步了，比我们付出的学费更为重要。文明就是我们是不是学会了更多的妥协和敬畏。<br />
<br />
这里这么多革命呼唤者，正好最近看了崔卫平的《正义之前》，把书后的这几句话与大家共享一下：<br />
““正义”是个多么诱惑人的字眼。但是，正义到来之前，在行正义之前，我们需要做哪些细致、复杂的准备？要求学会仁慈、学会宽容、学会用“复眼”看待这个世界，了解这个世界的全部复杂性，以及我们自身的种种有限性。”<br />
<br />
换而言之，革命之外，我们是不是在思考仁慈、宽容，他们决定了革命后的方向。<br />
<br />
美国革命、台湾、日本，都有其特定的历史背景和条件。我不是历史学家，不敢妄言，我说的坐标连一家之言都算不上。就不多说了。<br />
<br />
但是我的基本判断是中国付出的代价比他们大的多。如果有不同意见，我也不打算回了，仅仅是个人观点，而且也不是我关心的重点。<br />
<br />
<br />
我认为真正可以做的是“日拱一卒”，只要方向正确，对社会就有贡献。这个崔卫平的书中也有描述，谈到波兰的反对派，她说他们“目的和手段得到了统一，每一个手段同时也是它的目的”。<br />
