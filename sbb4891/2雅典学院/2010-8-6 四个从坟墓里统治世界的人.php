													<h2>四个从坟墓里统治世界的人</h2>
						
						
																											<div id="postmessage_771841" class="t_msgfont">作者：Bill Nugent (<a href="http://www.gracelawandsonship.com" target="_blank">http://www.gracelawandsonship.com</a>) <br />
诚之&nbsp; &nbsp; 译<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;在后现代的西方文明社会中，大多数人都会宣称自己是思想自由的人，但是实际上他们却被禁锢在四个已经死在坟墓里的人的思想里面。这四个人分别是：达尔文（1809 - 1882），马克思（1818 - 1883），尼采（1844 - 1900），和弗洛伊德（1856 - 1939）。这四个属于19世纪的人是西方文明的破坏者。他们的思想毒素挑战传统的基督徒思想，并诱使欧洲和美国离开基督教信仰，进行一项庞大的社会实验。 <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;上一代哲学家在聊天喝茶时的谈资，会成为下一代人在大学时被教导的素材。再到下一代，就列为高中的课程。然后进入流行文化，最后甚至变成小学的教材。 <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;达尔文的无神进化理论侵蚀了千万人的基督教信仰。马克思，尼采和弗洛伊德深受达尔文的影响。达尔文主义主张人类不过是猿猴的后代，是没有灵魂的，死后也不会有意识存留，而这个理论就成为构筑整个世俗知识界雄伟建筑的基础。 <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;讽刺的是，帮达尔文写传记的人坦率地承认，达尔文并没有对他1859年的原著《自然选择下的物种原始》（The Origin of Species by Means of Natural Selection）提供什么原创的想法。进化论作为一个自然历史的理论，在早他两代之前就已经出现了。更早的作家甚至已经讨论过自然选择。达尔文自己的祖父伊拉姆斯&#8226;达尔文，就已经写到了进化论。 <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;在达尔文之前，演化论被局限在知识界精英的角落里。达尔文之所以出名，是因为他把所有零星的问题整合起来，并且用一种井井有条的方式加以呈现，使得这个思想在受过教育的人当中如星火燎原般散布开来。 <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;达尔文也因为他那本1871年的书，《人类的由来》中的极端反叛和种族主义立场而出名。这本书是为了对抗批评他1859年的书而写的，把他的思想，即人类是猿猴的后代，完整且大胆地发展出来。 <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;很不幸地，《人类的由来》的种族主义立场是那么地露骨，宣称某些棕色人种的民族比欧洲人的进化要慢。以生物学为基础的种族主义从中得到巨大的动力，并受到知识界的推崇。在澳洲，土著民族像动物一样被猎杀；比利时人在刚果射杀黑人土著完全不用担负刑责。希特勒在他的书《我的奋斗》中，广泛地引用达尔文书中的话，并从达尔文主义中发展出亚利安种族优越论的宣称。 <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;在19世纪的美国，演化论只在知识界的菁英中被教导和讨论。演化论只在大学水平，以及在某种程度上，在高中被教导。在1880年代，只有大约4%的美国学生上高中，因此被称为次级学校（secondary school）。只有大约1%的人口可以上大学。其他所有的人都在基础学校受教育，直到8年级。 <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;在美国，受高层教育的人变得越来越世俗化，而小学仍然持守传统的基督教价值。这导致上层社会和平民大众之间的隔阂，在某种程度上，这个隔阂持续到当代。世俗人文主义成为菁英界的主要信仰。达尔文主义是其神话的源头。</div>

							
							
							
							
