为什么这么说呢？<br />
因为马克思对资本主义的彻底诋毁，使他的经济学意见完全脱离了现实。这种情况下，马克思主义或者不实施，仅仅作为一种理论；或者一实施就受挫，一受挫就退缩，结果和不实施也没什么区别。这些情况下，马克思主义当然可以保持其本意，但也肯定不能掌权。<br />
如果非要掌权呢？一种在经济上完全脱离现实的，却是基于经济决定论的理论，要想一直实践下去，除了依靠赤裸裸的暴力以外，还能依靠什么呢？</div>

							
							
							
							
