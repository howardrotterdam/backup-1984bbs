																											<div id="postmessage_771845" class="t_msgfont">达尔文和马克思之后一代，一个更大胆更有热情的无神论哲学家兴起了。他的名字是尼采。他相信，人最主要是被支配他人的欲望所主导。尼采称这个欲望为“权力意志”。 <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;尼采是个存在主义哲学家。存在主义是一种极端的个人主义，认为个人必须运用他个人的意志，抛弃社会习俗，以创造他自己独特的生命。他把那些根据规则而活的人视为不真实的人，只是盲从者，缺乏雄心大志。 <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;尼采拒绝上帝的律法，并且写了一些渎神的作品。他对政治也有相当极端的看法。 他认为古代所有的伟大文明都是建立在奴隶和暴虐的基础上的，知识界菁英用许多人的辛苦劳力，换来他们的奢靡生活。但是他却将它合理化，说奴隶和压迫是好的，因为它能让菁英分子得到释放，可以建造宏伟的建筑，创作伟大的艺术作品。希特勒广泛地阅读尼采，甚至把尼采的书送给他部队的军官。 <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;尼采的存在主义和无神论对他当时的知识分子具有很大的影响力，并且在我们这个时代中回响，特别是在后现代主义中。尼采影响了存在主义哲学家沙特，卡缪，以及二战后的悲观主义。1960年代的叛逆青年，就有很多尼采的极端存在主义的元素。</div>

							
							
							
							
