													<h2>旧约圣经申命记第七章开始部分</h2>
						
						
																											<div id="postmessage_773652" class="t_msgfont">&lt;&lt;&lt;&lt;&lt;&lt;<br />
1 “耶和华你上帝领你进入要得为业之地，从你面前赶出许多国民，就是赫人、革迦撒人、亚摩利人、迦南人、比利洗人、希未人、耶布斯人，共七国的民，都比你强大。<br />
2 耶和华你上帝将他们交给你击杀，那时你要把他们灭绝净尽，不可与他们立约，也不可怜恤他们。<br />
3 不可与他们结亲。不可将你的女儿嫁他们的儿子，也不可叫你的儿子娶他们的女儿；<br />
4 因为他必使你儿子转离不跟从主，去事奉别神，以致耶和华的怒气向你们发作，就速速地将你们灭绝。<br />
5 你们却要这样待他们：拆毁他们的祭坛，打碎他们的柱像，砍下他们的木偶，用火焚烧他们雕刻的偶像。<br />
6 因为你归耶和华你上帝为圣洁的民；耶和华你上帝从地上的万民中拣选你，特作自己的子民。<br />
7 “耶和华专爱你们，拣选你们，并非因你们的人数多于别民，原来你们的人数在万民中是最少的。<br />
 &gt;&gt;&gt;&gt;&gt;&gt;<br />
这一部分内容，包括了种族主义和宗教极端主义的含义。<br />
当然，这不是说信仰《圣经》的人就一定是种族主义和宗教极端主义的人。人们大可以用更为合理的方式来信仰《圣经》。<br />
但是，在历史上，人们宗教极端主义行径的源头，难道不是在这类地方吗？<br />
一神教的不宽容性难道不正是思想罪的源头吗？<br />
种族主义的传承倒不是很好说，因为犹太人始终不够强大，而随着基督教传入万邦，《圣经》中上帝选民的种族反而成了外族。这样一来，逻辑关系就不那么明显了。<br />
不过，在当今的语境下，种族主义是用来骂人的。由于二战和联合国宪章的缘故，种族屠杀被公认是最大的罪恶，一开始这个仅仅指真正的种族屠杀，后来则变成了沾边就算。比方说红色高棉，也被控以种族灭绝。而自美国放弃种族主义以后，种族主义也变得十恶不赦和沾边就算了。<br />
广义的种族主义，比如历史上：白人的优越地位，基督徒的优越地位，对异教徒的歧视杀害，显然也可以在《旧约》中找到源头。（希特勒那种有达尔文主义背景的种族主义确实和《旧约》无关）</div>

							
							
							
							
