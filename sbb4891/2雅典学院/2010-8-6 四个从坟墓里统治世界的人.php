&lt;&lt;&lt;&lt;&lt;&lt;<br />
马克思和达尔文是同时代的人，互相熟悉彼此的作品。马克思受到达尔文的影响，在他的书，《资本论》中提到了达尔文。马克思接受一种经济决定论的思想。他把经济上的获利当作是人类这个动物的强烈推动力。他把人当成动物，只是运动中的原子。<br />
&gt;&gt;&gt;&gt;&gt;&gt;<br />
这些部分粗略地讲，还算对。当然马克思也有人道主义精神，但因为他确实“把经济上的获利当作是人类这个动物的强烈推动力&quot;，所以难免遭此恶评。要知道，马克思最著名的书是《资本论》，而这无疑说明他对经济问题看得极重。<br />
说马克思受达尔文影响，也是有道理的，只不过”互相熟悉彼此的作品”，有抹黑达尔文的嫌疑。貌似达尔文这个人很内敛，又不是这个行当的，身体也不好，未必有兴趣去熟读马克思的作品。就算读了，也绝不可能赞成，何必提及呢？<br />
&lt;&lt;&lt;&lt;&lt;&lt;<br />
马克思接受黑格尔的观念，认为人类政府比它各部分的总和来得大。因此，我们就有了马克思的怪论，就是把个别的人当成是没有价值的，但是如果把个别的人放在政府里，你就具有了真正价值的东西。人类政府被马克思当作人类的救主，会把人带到乌托邦社会。 <br />
&gt;&gt;&gt;&gt;&gt;&gt;<br />
这个说法抹黑马克思的程度要更大一些。马克思不大可能像黑格尔那种的反动派那样认为政府如此重要，一个不掌权且反政府的人，如果如此推崇政府，岂不等于是藐视自己？<br />
比抹黑马克思更可气的是，文章就这么从黑格尔引申到了救主、乌托邦，而救主、乌托邦的概念，显然源自基督教的弥赛亚（救世主）和天堂，而绝非源自黑格尔。（虽然黑格尔也不是什么好东西，但是借着他为基督教开脱总是不行的）<br />
&lt;&lt;&lt;&lt;&lt;&lt;<br />
马克思的著作激发了几代人的革命，从事人类有史以来最大型的经济社会实验。这个实验是想看一个具有完全的权力，拥有全部财产的政府，是否会创造一个完美的乌托邦社会。但是这个实验彻底失败了。历史家估计，在寻求这个乌托邦社会的过程中，至少有七千万到一亿人因此丧命。 <br />
然而，马克思的理想仍然主导着所有欧洲、北美和南非现代民主政府的社会主义倾向。<br />
&gt;&gt;&gt;&gt;&gt;&gt;<br />
这部分内容也是偏的，虽然貌似有理。它主要的特点是要用马克思主义来抹黑一切左翼倾向，而且针对的就是左翼要求政府干预经济这部分内容。但实际上并不是如此。共产主义的试验，造成最大灾难的，往往不是由政府来主导一切，而是政府怂恿群众去发挥能动性。</div>

							
							
							
							
