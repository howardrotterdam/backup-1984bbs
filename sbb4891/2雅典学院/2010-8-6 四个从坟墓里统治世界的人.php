马克思主义是最讲经济决定论的――这个观点可直接参考尼克松的书《1999不战而胜》里对一些列马克思主义国家实践上的巨大失败与理论之间反差的嘲笑。尼克松是认真阅读过大量马列著作的少数西方政治家之一。尼克松的马列功底可能比现在绝大多数TG党员要好。关于对马克思主义最鲜明的色彩之一“经济决定论”的评述，这种东西汗牛充栋，对这点也跳出来质疑，未免螳臂当车。<br />
<br />
现在一般语境里所理解的马克思及马克思主义是指马克思列宁这一系流传下来的，而非法兰克福学派西马，也非托洛茨基和第四国际这些被多数人认为由异端色彩的分支。马克斯本人对未来无产阶级统治形式的设想是非常模糊的，有本书乔万尼.萨托利《民主新论》里有一章讲马克思所理解的民主这个问题时，专门考究过马克思对未来理想社会里统治具体指指形式的设想，遍寻他的著作，发现马克思本人对这个问题几乎没有谈论过，马克思很笼统的主张无产阶级专政，同时又反对作为暴力工具的国家机器。做过大量论述的是非常欣赏普鲁士式官僚机构的列宁。楼主文章里说的利维坦式的马克思主义政权，应该是列宁的主张并有由斯大林具体完善。20世纪一系列马克思主义国家实体正是依据马克思提供的哲学基础，列宁提出的具体模型，斯大林加以实践并完善的模式建立的。<br />
<br />
由此说来，楼主文章里说的一点也不错，哲学上鼓吹的经济决定论，设想的乌托邦要通过一个霍布斯的利维坦式国家来实现。而实践中确实灾难性的后果，证明了当初马克思经济均定论为突出色彩的那一整套理论的破产。</div>

							
							
							
							
