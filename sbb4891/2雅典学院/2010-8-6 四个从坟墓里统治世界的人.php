这个是没错的。<br />
马克思的《资本论》，完完全全是经济决定论的，也完全把“经济上的获利”当做人类的强烈动力的。<br />
其实，也不奇怪。《资本论》至少在形式上就是一本描述经济规律的著作，经济规律当然要重视经济，而且，所谓”经济人“的假设，也就是可以理解为&quot;把经济上的获利当作是人类这个动物的强烈推动力。&quot;<br />
那么，”劳动异化“这个概念又是怎么回事呢？<br />
马克思认为，在资本主义条件下，人们就是把获利作为强烈动力的，就是经济决定论的，而这个是不人道的。<br />
由此我们可以看到，马克思把人类视为经济动物，和“劳动异化”的观念并不冲突。<br />
当然，你可以说：马克思把（资本主义国家的）人类视为经济动物，但马克思也反对人类成为经济动物啊！为什么不强调后一条呢？<br />
这是因为，马克思用来解决”劳动异化“的方法，还是纯经济的。马克思并不是说：我们牺牲一部分经济效率，以防止劳动异化。马克思说的是：我们改良经济运行模式，用比资本主义更高效的方式来运转经济，同时防止劳动异化。<br />
马克思真正重视的，仍然是经济规律，而不是人性本身的需要。当然，他认为，两者是一体的，经济模式良好了，人性的需要自然就可以满足。但这难道不就是把人类视为经济动物的最明显的体现吗？</div>

							
							
							
							
