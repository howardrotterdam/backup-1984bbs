													<h2>回复 39楼 北京棋迷 的话题</h2>
						
						
																											<div id="postmessage_775133" class="t_msgfont">资本主义的内在问题，如果仅从劳动异化入手，马克思说得当然不错。<br />
但别的就都不然了。而劳动异化，马克思又不打算直接加以解决：比方用福利国家的方法减少劳动对人民的逼迫，等等；而是妄图用一个以经济为基础的整体方案来解决。由于我们只能根据手段而非目的来为政治家分类，这就只能说他是经济决定论者。<br />
马克思本人深入政治，《共产党宣言》《法兰西内战》等等，根本不是在针对等几百年的后事。更何况他的掘墓人等很多理论太具体，如果说“谁能说几百年后，资本主义不灭亡？”，那么就更该说“谁能说几百年后，资本主义会像马克思说的那样灭亡？”<br />
归根到底，马克思不是一个像尼采那样的不提具体路线的纯理论家或哲学家。他说的话太具体，错了就不能被开脱。<br />
最后，马克思经济观点中的价值理论，把价值完全归结于劳动，而完全否认了投资可能创造价值。这就使他的经济理论成了经济发展的敌人。因为否定投资的价值，就等于否定投资，而不投资，哪来的发展？</div>

							
							
							
							
