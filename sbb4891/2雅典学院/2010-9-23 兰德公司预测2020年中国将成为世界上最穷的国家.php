<br />
&nbsp; &nbsp; 声明：此报告不代表任何政治立场，只具有其参考价值，思考比评价更重要。<br />
<br />
&nbsp; &nbsp; 兰德公司报告<br />
<br />
&nbsp; &nbsp;（一）中国人不了解他们作为社会个体应该对国家和社会所承担的责任和义务。普通中国人通常只关心他们的家庭和亲属，中国的文化是建立在家族血缘关系上而不是建立在一个理性的社会基础之上。中国人只在乎他们直系亲属的福址，对与自己毫不相关的人所遭受的苦难则视而不见。<br />
<br />
&nbsp; &nbsp; 毫无疑问，这种以血缘关系为基础的道德观势必导致自私，冷酷，这种自私和冷酷已经成为阻碍中国社会向前发展的最关键因素。<br />
<br />
&nbsp; &nbsp;（二）中国人是世界上少数没有信仰的可怕国家之一。<br />
<br />
&nbsp; &nbsp; 中国人没有自己的信仰，这导致自我泛滥，缺少约束，缺少统一的精神支柱，他们没有通一规范的道德意识和真理意识，每个人只相信他自己，每个人按自己的意志确立行为，每个人都有他自己行为的道理，这导致中国人在各个行业和生活领域中没有统一意识。<br />
<br />
&nbsp; &nbsp; 中国是无神论国度，大多数人接受的是无神论者教育，蔑视宗教信仰，把宗教信仰当做迷信，精神领域没有共同的凝聚和约束。大部分人，他们坚持认为自己是无神论者，其实质行为上却是泛神论者，在中国，拜鬼现象非常普遍。<br />
<br />
&nbsp; &nbsp; 由于缺乏信仰，中国人没有罪恶感，没有亏欠和内疚感，只要犯罪不被知道，就是无罪，这导致中国人在内部矛盾分歧时，在人性中的残忍和冷漠，纵观中国整个历史，最残忍的争斗和屠杀都来自于他们内部自己。<br />
<br />
&nbsp; &nbsp;（三）中国人所说的政治除了欺骗和背叛没有其它东西。<br />
<br />
&nbsp; &nbsp; 中国从来就没有成为一个法制社会，因为中国人的思维方式与守法行为格格不入。中国人老想走捷径。他们不明白这样一个事实：即成就来自于与努力工作和牺牲。在中国，人情高于法律，导致一代又一代人在徇私枉法贪赃受贿的社会不公正和法律不公正中互相效法模仿，徇私枉法成为中国人的传统。在中国，政治斗争是罕见残酷而无情的，政治斗争让中国一代一代人失去人性。<br />
<br />
&nbsp; &nbsp;（四）大多数中国人从来就没有学到过什么是体面和尊敬的生活意义。<br />
<br />
&nbsp; &nbsp; 中国人倾向于索取而不给予。他们需要明白一个道理：生活的真蒂不在于你索取多少而在于你能给予社会和你的人类同胞多少。大多数中国人从来就没有学到过什么是体面和尊敬的生活意义。中国人普遍不懂得如何为了个人和社会的福址去进行富有成效的生活。潜意识里，中国人视他们的生活目的就是抬高自己从而获得别人的认知。这样一来，一个人就会对&quot;保有面子&quot;这样微不足道欲望感到满足。&quot;面子&quot;是中国人心理最基本的组成部分，它已经成为了中国人难以克服的障碍，阻碍中国人接受真理并尝试富有意义的生活。<br />
<br />
&nbsp; &nbsp; 这个应受谴责的习性使得中国人生来就具有无情和自私的特点，它已成为中国落后的主要原因。中国人没有勇气追求他们认为正确的事情。首先，他们没有从错误中筛选正确事物的能力，因为他们的思想被贪婪所占据。再有，就算他们有能力筛选出正确的事情，他们也缺乏勇气把真理化为实践。<br />
<br />
&nbsp; &nbsp; 中国人习惯接受廉价和免费的事物，他们总是梦想奇迹或者好运，因为他们不愿意付出努力，他们总想不劳而获。很少有中国人明白一个事实，就是威望和成就是通过一步步努力的工作和牺牲实现的，不付出就没有所得。简单来说，如果是为了谋生，那一个人只有去索取；但如果是为了生活，一个人必须要去奉献。<br />
<br />
&nbsp; &nbsp; 为了掩饰中国目前的失业，贫穷，惊人的资源浪费等现实，中共政府编造出一串数字来把中国打扮成一个经济上欣欣向荣的国家。这样做只有一个目的，就是吸引更多的外国资本来填补中国的这个无底洞。所以当人们下次听到中共政府所宣布的经济高增长率等数字时应该想想这些臆想出来的数字是否正常。我们不能忘记我们不得不为这个中共政府粉饰的表面上繁荣的伪资本主义国家付出什么样的代价。<br />
<br />
&nbsp; &nbsp;（五）中国人的价值观建立在私欲之中。<br />
<br />
&nbsp; &nbsp; 由于在贫穷的环境下生长并且缺少应有的教育，大多数中国人不懂得优雅的举止和基本的礼貌。他们中的大多数人着装笨拙粗鄙却不感到害羞。他们在青少年时所受的教育就是如何说谎并从别人那里索取，而不是去与别人去分享自己的所有。<br />
<br />
&nbsp; &nbsp; 中国是一个物产丰富的国家。但无限制生育政策所带来恶果使得中国成为了无限廉价劳动力的输出国。这些输出也包括那些受过教育的劳力输出，除了他们的教育水平，实则和其他一般苦力没有本质上的区别。<br />
<br />
&nbsp; &nbsp; 中国大规模生产的便宜产品降低了输入这些产品的地区的商业信用度。由于技术落后，管理失败，中国制造的单位能耗要比发达国家如日本，美国高出很多。因此，随着出口额的增加，中国在扩大生产的同时丧失着宝贵的能源。同时，这种行为也严重的污染了环境，使中国变为全世界最不适宜人类居住的国家。　　<br />
<br />
&nbsp; &nbsp; 目前中国正在遭受着资本主义社会两大邪恶的折磨，即环境的破坏与人性的丧失。由于中国人天生的贪婪的本性，它们可以毫无保留的接受资本主义的阴暗面和无止境的利益纷争。　　<br />
<br />
&nbsp; &nbsp; 中国人对西方的技术与产品狂热追求却对西方管理文化所强调的坦率，直接，诚实这些品质漠不关心。有人曾经做过的一个精彩的比喻可以用来解释这种奇怪的被称为中国模式的资本主义的现实，即没有法律约束的资本主义就像不存在地狱这一概念的基督教，最终每个人都堕落，无一幸免。事实上，现在的中国制造已经成为质量低劣，价格便宜，仿制，侵犯知识产权的代名词。　　<br />
<br />
&nbsp; &nbsp; 由于中国文化不鼓励敢于冒险这种优良品质，所以中国人极力避免冒险，他们也不想寻求机会来改善自己的生活。<br />
<br />
&nbsp; &nbsp;（六）中国人的生活思想还停留在专注于动物本能对性和食物那点贪婪可怜的欲望上。　　<br />
<br />
&nbsp; &nbsp; 中国人对于生活的平衡性和意义性并不感兴趣，相反他们更执迷于对物质的索取，这点上要远远胜于西方人。大多数中国人发现他们不懂得&quot;精神灵性&quot;，&quot;自由信仰&quot;以及&quot;心智健康&quot;这样的概念，因为他们的思想尚不能达到一个生命（补：即肉体和灵性的并存）存在的更高层次。　　<br />
<br />
&nbsp; &nbsp; 中国人追求腐化堕落的生活，满足于自我生理感官需求，他们的文化建立在声色犬马之中:麻将、赌博、色情、吃欲、贪欲、色欲无不渗透在他们生活和文化中。<br />
<br />
&nbsp; &nbsp;（七）失败的中国式教育成为世界的笑柄。<br />
<br />
&nbsp; &nbsp; 在中国人的眼中，受教育不是为了寻求真理或者改善生活质量，而只是身份和显赫地位的象征和标志。中国的知识分子从别人那里得到尊敬并不是因为他们为了别人的幸福做过什么，而只是因为他们获得占有了相当的知识。事实上，他们中的大多数只不过是一群仅仅通晓考试却从不关心真理和道德的食客。　　<br />
<br />
&nbsp; &nbsp; 中国教育的目的不是为了帮助社会改善总体水平，而是为统制阶层和少数富有阶层服务的。中国的政府部门总是引以为豪的标榜自己是人民的这个，人民的那个，但没有一个是为人民的利益服务的。唯一服务的对象就是他们自身集团的利益。　　<br />
<br />
&nbsp; &nbsp; 中国的教育体系很大程度上已经成为一种失败和耻辱。它已经不能够服务于教育本应所服务的对象：社会。这个教育体系不能提供给社会许多有用的个体。它只是制造出一群投机分子，他们渴望能够受益于社会所提供的好处却毫不关心回报。　<br />
<br />
&nbsp; &nbsp; 中国可以培养出大批的高级能人才，但却很少可以培养出合格的可以独立主持的管理级专家。服务于一个公司或者社会，光有技术是不够的；还需要有勇气，胆量，正直和诚实的领导才能，这恰恰是大多数中国人所缺少的品性。正如亚瑟.史密斯，一位著名的西方传教士一个世纪前所指出的，中国人最缺乏的不是智慧，而是勇气正直的纯正品性。这个评价，虽然历经百年，如今依旧准确诊断出中国综合症的病因。</div>

							
							
							
							
