													<h2>记96岁国民党抗战老兵</h2>
						
						
																											<div id="postmessage_690077" class="t_msgfont">老兵不死，只会慢慢凋零--------------记一位抗战老兵&nbsp;&nbsp;<br />
日志原文：<a href="http://selflocking.blog.sohu.com/152444507.html" target="_blank">http://selflocking.blog.sohu.com/152444507.html</a><br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
<br />
　实际上，在我写这篇文章之前。我都在想其他问题。例如民主政治、道德体系、人本主义、民族、国家等等乱七八糟的东西。我也不知道我会为什么会去想这些，也不知道为什么会写下来。因为这些话题和我写这篇文章的初衷似乎无多大关系，但似乎又有点关系。总之有点理不顺的意思。不过这只是我的一点感想，权作引子。<br />
<br />
　言归正传。这件事情离现在有段时间了，5月8号，我接待过一位抗战老英雄。老英雄是黄埔军校十六期学生，由于日寇侵华。为了保卫家园，毅然从戎。曾经参加过著名的台儿庄战役、保卫大汉口、保卫大徐州、山西会战以及中原会战。49年国祚迁移，不得已黯然去台。两岸铁幕，虽然思念家中妻儿，也只能饮恨远眺，直到1989年才有机会回国探亲，时间无多，驻留一段时间之后又返回台湾，直到近日回家。由于我一直对政治历史比较兴趣，因此对任何有关于历史的资料，我都非常的关注！遗憾的是，我最期待的能够零距离了解历史的机会，却一直没有。这种感觉这并非完全出自于兴趣，还有一点是我希望有一天能够向曾经为我们这个民族和国家付出重大牺牲的英雄，当面致敬！遇到老英雄，实在太让我意外。<br />
<br />
　和老先生在一起的时候，老先生一直都在说爱国、良心、血性。当然还有抱怨！说真的，在现在的大陆，说什么爱国血性良心，别人如果不是觉得你是作秀，就肯定是神经病。当时我也是这样想的，现在回想起来总觉得很龌龊。我想看文章的诸位，心中多少也闪过这个念头吧！当时我狠狠的摁住胸口，不断提示自己。老先生和我们不是一个年代的人，他们那时候是热血沸腾的年代，他们是战场上饮血抗敌的英雄，他们不知道大陆早已经不是曾经的温良恭俭让，而是世风日下的冰河时代！老先生说话的时候很慷慨，丝毫没有顾忌到我的表情，如果他仔细关注，应该会察觉出来的。<br />
<br />
　“老先生，中正老先生和蒋经国总统难道没有照顾你们吗？”我问道。<br />
<br />
　“当然有，不过他们都死了啊，现在是民进党，我是反台独的，还被他们关了两个星期，说我思想有问题，我住在高雄，是民进党的地方。国民党在台北，管不着！”老先生回答道。<br />
<br />
　“政府太对不起我们啦！我96岁，连回家的路费都没有，身上500块钱还是两个人给的”，老先生满脸的哀伤，我静静的听，一言不发。“还好有500块，够我回家的路费了，人老了，该回家了。以前在老家建过两间房子，总算有个窝。我家里还有个外甥，他叫张国茂。”老先生笑了起来，由于年龄很大，脸上的肉都已经松散，叠在脸上，看起来很慈祥，也有点很天真的感觉。我忍不住问了一句：“对了，老先生你回家，家里人知道吗？”<br />
<br />
　“他们不知道，我都站不起来，天天坐着轮椅。我家里人都死光了，我叔叔伯伯爸爸都死了，我有个哥哥，参加打仗，结果也被打死了。”老先生右手比划了一个手枪的姿势。由于老先生听力不好，有点耳背，我问他的话，要很大声他才听见，听到之后他就会回答我，说起话来嘟嘟囔囔，有点听不清楚，而且还常常偏题。不过对于我来说这些都没有什么关系，我只想他说的更多点，因为那是一部活生生的历史。老先生说，我听，突然我闪过一个问题，于是赶紧问道：“老先生，当年共产党和你们的关系是什么样子？你能不能仔细说说”<br />
<br />
　“我们关系不好啊，我们打仗他们也打，但是他们打得不多，都是我们打。我回家那年有个小伙子说是共产党打仗，我当时就说是国民党打仗哇！共产党在背后打我们，如果不是我们打仗，你早就做亡国奴了！”老先生很激动，手在空中挥舞。<br />
<br />
　突然他想到什么了，指着他的那个破皮箱，要要我打开。皮箱的拉链不好，我怕弄坏，折腾了好久才打开。里面都是很厚的衣服，和老先生穿的一样，又厚、又旧、又脏。还几根卷轴，以及一个蓝色的文件夹，文件夹上面贴了一枚青天白日徽章，下面有一行手写字：良心未泯、血性未汩中国人刘玉纯。随手翻开夹子，里面都是一张张的竖写公文，其中有行政院秘书回信，有蒋经国亲笔信函，有李焕、许历农、林洋港等人的回复。信笺上统一都是行政院书函和中央常务委员会字样。除此之外还有一些照片，里面有一张老先生年轻时期的照片，看起来英武挺拔，正气决然。除此之外还有与蒋先生、黄杰将军等人的合影照片。夹子里还夹着一本回忆录。<br />
<br />
　老先生招呼我一下，让我把回忆录递给他。他拿着那个本子，翻开跟我说:“你看，这是我的回忆录，这里就是以前tg党怎么坑杀我们同志的。”<br />
<br />
　我仔细看完那段话，内容说的是他们一个中队被tg军俘虏，结果惨遭坑杀，仅一人逃脱的事情。除此之外我还看到另外一段内容，因为他是营长，上级交代有几名tg军俘虏，要处决。他看到几个俘虏衣衫褴褛，于是放了几声空枪，随即把人放走。老先生满脸的慈祥，“我从不滥杀无辜，他们也是人啊！你看我这回忆录最后面的，有忠，有孝，有血，有泪......” 我突然想起网上很多关于日本人的言论，于是问道：“我看到很多文章说当年日本人侵华，只要中国人服帖，日本人也不会杀戮，是不是真的，老先生你有没有亲眼见过？”<br />
<br />
“胡说八道，我就亲眼见过日本人怎么杀人的，我们部队到一个村里，日本人之前在这里杀过人，现在走了，我们清场的时候，我就看到一个老太婆和一个小女孩的尸体，被强奸了，身上的衣服都没有，胸部也被刺刀捅过，都是血，肠子都流出来了。日本人每到一个地方都是这么干的，村子里年轻力壮的都躲起来了，留下老人来看守房子，没想到也全死在刺刀下，这是我亲眼所见，真的很悲惨啊！......”<br />
<br />
　由于工作关系，我不能呆的太久，只好跟老先生说：“我还有点事情，我先走了，待会再来看你，你要有什么事情的话，打那个床头电话，拨8555就好了。”然后我把号码写在纸上，再说一遍，让他记得更清楚。走出房门前，我停了一下，然后转身向老先生端端正正敬了一个军礼。<br />
<br />
