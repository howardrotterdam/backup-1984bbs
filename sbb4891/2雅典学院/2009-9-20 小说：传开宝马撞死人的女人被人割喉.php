<br />
庭审中，韩少虹辩称，自己虽然很早便获得机动车驾驶执照，但并未参加过机动车驾驶理论学习，这辆宝马吉普车她是第2次开上道，对车的性能并不熟悉。当日她本是想倒车，不想车却向前冲去，连撞13人后，直撞到大树上才停下。当审判长询问她是否会用手刹车时，韩少虹表示不会使用。<br />
<br />
这些辩词被法庭采纳，并据此对韩少虹进行了从轻处罚。<br />
<br />
<br />
3、愤慨：公众质疑连绵不息<br />
<br />
上述判决公布之后，立刻引起了广泛的质疑声。而某杜姓法律工作者从专业的角度提出以下疑点：<br />
<br />
<br />
质疑一：12位证人没有到庭<br />
<br />
据录像资料显示，在庭审过程中，公诉人共出具12份证人证言，无一人当庭作证。杜认为，目击证人的证言的真实性需要进一步核查，要当庭对证。<br />
<br />
<br />
质疑二：韩少虹是不是故意杀人？<br />
<br />
警方的分析是&quot;韩当时确实比较生气，但她对周围的12名群众没有任何利害冲突，所以从这一点看，韩没有故意撞人的动机。&quot;<br />
<br />
杜指出，12名无辜被害人受伤是由于汽车高速行驶后产生的巨大惯性导致的，即使能证明韩少虹没有故意撞那12个人的动机，但并不能证明其没有故意撞邓美芳的心理活动。<br />
<br />
更加关键的是，外界对肇事者撞人动机的怀疑源于一个有待澄清的事实：韩少虹在上车前说了什么？而有事发者称，韩少虹上车前嘴里&quot;嘟囔&quot;的是：信不信我敢撞死你。<br />
<br />
<br />
质疑三：韩到底会不会开车<br />
<br />
按照交警部门的鉴定结果，韩少虹在事发当时有两个错误的操作。一是挂错了挡；二是，她的右脚从刹车踏板上抬起，踩向了油门，而这一切都是因为 &quot;慌乱&quot;。但宝马公司的技术人员认为，一般情况下，车辆启动时，大部份驾驶员的右脚会在挂挡之后继续留在刹车踏板上，而不会移动到油门位置。发生误挂挡位，车辆突然提速冲出的情况还是有可能发生的，但发生的几率微乎其，如果是发生在一个常开车的老司机身上，那简直是&quot;不可思议&quot;的。<br />
<br />
韩少虹所持的驾驶证是1997年核发的，驾龄应该是六年。在交警部门最初的询问中，韩少虹的回答是&quot;经常开车&quot;。而在法庭上，韩说她不会开车。<br />
<br />
面对此起彼伏的质疑声，韩少虹虽然被轻判缓刑，但是正常的生活也被打乱。半年之后，她离开了H市，迁居到千里之外的A市，试图籍此摆脱人们的视线，亦即摆脱舆论带给她的巨大压力。<br />
<br />
<br />
4、意外：他乡突遇死亡威胁<br />
<br />
韩少虹来到A市之后，因为当地并没有人认识她，她的生活的确慢慢地归于平静。可没想到在数年之后的8月22日，她却忽然收到了一封匿名信，发信人自称 Eumendies。这个人在信中宣称：因为韩少虹数年前开车撞人的行为涉嫌故意杀人，而法律并没有对此进行公正的裁决。应广大网友的呼声，他以公正代言人的身份宣判韩少虹死刑，并且会在第二天，也就是8月23日执行这一判决。<br />
<br />
很显然，这是一封死亡威胁信。韩少虹立刻报了警。不久之后，市刑警队和特警队的警官就先后到来，他们共同为韩少虹设计了一套非常严密的保卫计划。<br />
<br />
得到警察的保护之后，韩少虹便定下心来。事实上，自从她开宝马车撞死人的消息传出去之后，她已经不是第一次受到这样的威胁了。尤其是她还在H市的时候，经常会有人给她发邮件，打匿名电话进行辱骂和恐吓。直到她搬到A市之后，这样的情况才没有继续发生。<br />
<br />
韩少虹觉得这次肯定是有人泄露了自己的行踪，所以才有无聊的人跟过来恐吓。她决定先应付一阵看看，实在不行的话，就再换一个城市居住。反正她有的是钱，不怕折腾。<br />
<br />
<br />
5、骇然：杀手闹市割喉夺命！<br />
<br />
8月23日这天，韩少虹仍然按照以往的生活习惯，早上八点起床，然后乘坐一辆红色的宝马车前往公司。迁居到A市后，韩少虹便投资经营着一家外贸公司，经过四五年的积累，已经有了一定的规模，收入颇丰。外贸公司所在的德业大厦位于闹市区的市民广场东南角，而韩少虹每天都会把这辆宝马车停放在大厦门外的停车场中。<br />
<br />
A市特警队的队长假扮成韩少虹的司机，当宝马车停好之后，他便一路守护着韩少虹进入大厦。同时停车场内还安排了警方的诸多便衣。大厦内也是如此，警方的便衣伪装成保安、保洁员甚至是公司白领，全方位的保卫着韩少虹的安全。<br />
<br />
韩少虹就这样在大厦里一直呆到了下午四点钟，这时她准备下班回家了。仍然和来的时候一样，由特警队长保护她穿过大厦前面的停车场。<br />
<br />
但是就在这个过程中，意外却突然发生了：当时从停车场外的不同方向上，突然冲进来十多个瘦小的男子，他们全都直奔韩少虹而来。警方的便衣立刻出手拦截这些男子，大部份男子没跑几步就被扑到在地上。只有三个人冲到了停车场里，不过特警队长把韩少虹挡在身后，那三个人根本没法靠近。<br />
<br />
韩少虹感到很害怕。这时她忽然看到不远处有个便衣站在安全的地方向自己招手，便连忙跑到对方身边。那个便衣扶着韩少虹上了宝马车，把车门锁好，然后又去追赶逃跑的可疑人员。当警方把这些冲过来的男子都制服以后，他们忽然意识到韩少虹已经很久都没有动静了。警察们连忙赶到宝马车旁，赫然发现韩少虹已经瘫倒在驾驶座上，她的脖颈上有一条又长又深的伤口，大量的鲜血正从伤口中流淌出来！<br />
<br />
韩少虹被立刻送往医院，不过在途中她已经伤重不治。后来警方在分析现场监控时发现，最后那个扶韩少虹上车的便衣根本就是假冒的！正是这个人在把韩少虹扶上车的同时下了杀手，他杀人的凶器根据分析应该是一个很薄很锋利的刀片！而杀完人之后，他假借追赶可疑人员，很快就消失在监控画面之外。<br />
<br />
而那十多个牵制警方注意力的瘦小男子，他们的来历则非常可笑。其中有一个叫艾云灿是市内某饭店的配菜小工。艾供述说：大约两周之前，他偶然看到张贴在街头的小广告：某大型娱乐中心招聘公关先生，待遇优厚，号称月薪可过万元。<br />
<br />
如此的高薪自然是个不小的诱惑，他拨通了广告上留的电话，接电话的男子告诉他：所谓的&quot;公关先生&quot;是要为女大款提供□□□□□□□□的。对方还通过网络给他发了张女客人的照片，对方竟是一个难得一见的成熟美女！艾云灿原始的欲望立刻被点燃了，他也按照要求给对方发了自己的照片，对方见到照片后也非常满意，并且立刻给艾的银行帐户上打了一千块钱作为他的&quot;前期准备费&quot;。<br />
<br />
收到&quot;准备费&quot;，艾云灿对这场特殊的&quot;招聘&quot;再无怀疑。他按照对方的要求购买了一些用具，然后便急切等待着美女客人的召唤。<br />
<br />
昨天下午，艾云灿终于又等到了那个男子的电话。对方说第二天就有生意，因为这样的交易是不合法的，那个女客人身份又尊贵，所以双方必须约好一种特殊且隐秘的&quot;接头&quot;方式。<br />
<br />
男子从网上发来了女客人乘驾的宝马车照片，然后告诉艾云灿，客人将于下午四点钟左右下班，到时候他必须在德业大厦外面的广场附近等待。当客人走出大厦后，他要及时跟上去，然后和客人一同上车。在这个过程中，他还将面对一些竞争者，最后能不能&quot;上岗&quot;还要看客人最终在现场的选择。<br />
<br />
夹杂着对金钱和美女的双重欲望，艾云灿完全按照男子的吩咐一步步地踏入这个游戏。当日下午三点四十五分，艾云灿乘坐出租车来到德业大厦，在男子电话指定的地点迫不及待地等候美女客人的出现。四点过后，照片上的美女--韩少虹终于走出了德业大厦，而艾云灿很快也得到了男子下车的指令。为了不让客人被其他竞争者抢走，他急匆匆地向着韩少虹奔去，然而没跑两步，就被警方的便衣按到在冰凉的地面上。他根本不明白到底发生了什么，直到做笔录的时候，他还一脸的茫然，以为自己是由于色情交易才被警方人员抓获的。<br />
<br />
其他被抓获男子的经历与艾云灿基本雷同。显而易见，那个张贴招聘广告，后来又与众男子电话联系的&quot;神秘人&quot; 就是这一连窜阴谋的策划者，同时也是杀害韩少虹的凶手。他虽然一直没有露面，但却一举控制了十多名欲望熏心的男子。这些男子全都成了他手中的提线木偶，在他精确之极的时间和地点指令下，这些男子纷纷冲入德业广场，把警方密不透风的埋伏圈冲得七零八碎，而真正的杀手则乘虚而入，伪装成警方的便衣完成了杀人计划。<br />
<br />
只可怜韩少虹自以为能逃过法律的制裁，不需要为自己的罪行付出代价。可到最后，她却终究未能逃脱最为严厉的惩罚！<br />
<br />
至于那个化名为Eumendies的杀手，A市警方正在对其进行全力追缉。<br />
