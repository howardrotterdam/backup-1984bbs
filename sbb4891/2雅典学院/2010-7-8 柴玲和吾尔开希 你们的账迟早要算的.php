<br />
请看历史事实：5月28日，当时的人大委员长万里在加拿大接见了美国和加拿大的中国留学生代表。留学生代表狠狠告了李鹏的状。说戒严法没有经过人大批准是违反宪法的，并要求万里取消政府的戒严法。万里答应一回国立即召开人大紧急常委会研究戒严法的合法性。一个很重要的事实是万里从来没有对留学生代表说戒严法是非法的，只是说研究研究。在中国的官僚制度下，研究常常是推托的代名词，所谓研究就是研而不究。另一个更重要的事实是万里说了“为了防止意外，建议学生还是先撤出的好”他要留学生代表转告。北美中国留学生联谊会立即把与万里谈话的详情FAX给北京高自联。但是谈话一到北京全走了样。高自联的头头首先把万里说的“为了防止意外，建议学生还是先撤出的好”的话删除了。剩下的话又变成了“人大委员长万里将回国主持人大紧急常委会，取消戒严令”，这条消息一广播，天安门广场上学生一片欢乐，大家沉浸在一种虚假的胜利之中。万里在５月３１日回国，到上海时就下了飞机并发表了支持戒严法的声明。天安门广场上学生情绪先是失望，继而一下子又愤怒到极点，骂万里变了立场。<br />
<br />
如果说柴玲，吾尔开希，李禄推翻了全体学生撤出天安门广场的决定是寄期望于万里取消戒严法，那么为什么在万里声明支持戒严法后还坚持留在广场上呢？难道他们不怕死吗？柴玲在64前的一次接见美国电视新闻的采访节目中有非常精彩的表现。我们看到节目播出是在大约89年6月7日左右。节目是专题介绍学生领袖柴玲的。其中有一段对话如下。美国记者问：现在已经戒严了，你们继续留在天安门广场上有没有危险呢？柴玲答：是的。我们有很大的危险。我很难过，我们的这些年青的学生们可能会为这场运动流血死去。说到这里。柴玲居然哭了起来。美国记者又问：那么你想不想死呢？柴玲一遍哭一遍答：不！我要话，我还很年轻。这段对话说明了两个事实：1.在万里发表了支持戒严法的声明后，高自联的头头柴玲，吾尔开希，李禄清清楚楚知道继续留在天安门广场上学生们可能会流血死去。 2.柴玲不想死的。看来吾尔开希，李禄也是不想死的。 柴玲现在口口声声说美国电视台错误地翻译了她的话。但是美国电视节目只是把她的声音略为降低，再加上英文翻译。英文翻译的声音要比原声延迟一至二秒。所以我们能很清楚地听到她讲：“不！我要活。”<br />
