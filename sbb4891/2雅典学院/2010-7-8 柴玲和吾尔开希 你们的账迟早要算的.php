稍微有点法律常识的人都知道，对抗戒严法的后果是死亡。在六十年代初，美国阿肯色的小石城，也就是当代美国总统克林顿的老家，白人种族主义者在开学时，不让黑人学生进学校读书，引起了一场全城性的骚动，由于骚动规模很大，警察无法控制，就实行了全城戒严。戒严期间，一些黑人团体成员违反戒严法继续进行示威游行，而遭到军警的枪杀。后来阿肯色的议会经过讨论，法律规定不许黑白学生分校。这场骚动才平静下来。事后，黑人团体要对枪杀示威游行者的军警起诉，州政府的回答是，死者是违反了戒严法，军警的开枪是合法的(justified).前几年，由于黑人骚动洛杉矶戒严时，军警对违反戒严法者也是格杀不论的。<br />
