																											<div id="postmessage_493115" class="t_msgfont">王思彤：“江泽民思想”成定局的先兆<br />
<br />
作者：王思彤&nbsp; &nbsp;发布时间：2010-2-25 17:30:35&nbsp; &nbsp;<br />
<br />
在中国共产党的历史上，“主义”、“思想”、“理论”都是极其重要而又敏感的字眼，特别是当它们与人名挂钩的时候。迄今为止，中国共产党认定也被大家接受的，只有马克思主义、列宁主义、毛泽东思想和邓小平理论。而一旦进入到这个层面，就意味着这些主义、思想或理论已经被中国共产党列为指导思想。&nbsp;&nbsp;<br />
<br />
　　“三个代表”重要思想是一个例外。&nbsp;&nbsp;<br />
<br />
　　不过，2010年2月24日的一条不同寻常的新闻“《江泽民思想年编(一九八九-二00八)》近日出版发行”却似乎透露出一个重要信号，那就是“江泽民思想”将有可能取代“三个代表”重要思想，并有可能列入党章。&nbsp;&nbsp;<br />
<br />
　　1943年7月8日，中共中央机关报《解放日报》发表了王稼祥起草的重要文章：《中国共产党与中国名族解放的道路----纪念共产党二十二周年与抗战六周年》。文章概述了党的历程特别是抗战以来的正确道路，指出我们党22年的历史，也是寻找、确定和充实中华民族解放正确道路的历史，并破天荒地提出了后来为全党所一致赞同的毛泽东思想。&nbsp;&nbsp;<br />
<br />
　　而这一次，江泽民思想的提出却别出蹊径，通过一本书来间接地产生影响。在此之前，学术界、理论界已经做了一些很好的铺垫，比如，一些正式出版的理论和学术著作是以“江泽民XX思想研究”的格式来命名的，比如，《江泽民执政党建设思想研究》、《江泽民文化思想研究》、《江泽民党建思想研究》、《江泽民国防科技工业思想研究》、《江泽民战略思想研究》、《江泽民国防和军队建设思想研究》、《江泽民军队管理思想研究》、《江泽民舆论导向思想研究》、《江泽民执政安全思想研究》、《江泽民文化建设思想初探》、《江泽民社会主义理论创新研究》、《江泽民人才思想研究》、《江泽民科技思想研究》、《江泽民实事求是思想论纲》，等等。这些著作涉及江泽民在经济、政治、文化、科技、军事等一系列重要领域的思想研究，为最终形成“江泽民思想”做了很好的理论准备。</div>

							
							
							
							
