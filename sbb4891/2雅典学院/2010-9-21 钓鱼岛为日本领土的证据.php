1984年2月23日，致力于社会人类文化研究的鸟越宪教授对外宣布：“对被认为是从云南南下的泰国山岳地带(泰国北部)的少数民族进行了实地考察，结果发现了所有婴儿的臀部有胎斑”。鸟越宪教授就这一发现又强调：“日本人在体质上所具有的胎斑渊源是云南，这是一个旁证，证实这个地区是日本人的发源地。” <br />
