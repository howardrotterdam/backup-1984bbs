1988年9月，日本电视工作者同盟抵达云南，其任务就是拍摄《日本人的起源》。 <br />
<br />
此后，日本学者们的推论又由“倭人起源于云南”，进一步演绎为“倭人的祖先为云南的少数民族”，其范围与核心，基本圈定为彝族、哈尼族、傣族等多种说法。 <br />
