													<h2>法轮功弟子印勇博士来函及方舟子按语</h2>
						
						
																											<div id="postmessage_806809" class="t_msgfont">方舟子，你好。<br />
<br />
　　读了你在《新语丝》发表的一系列有关法轮功的文章，以及你发起的宣言，作为一个法轮功弟子，觉得有必要说两句。顺便提一句，写这封信完全是自己的意愿。<br />
<br />
　　首先，我认为大家探讨问题是件好事，但我们应该避免采用谩骂、人身攻击、以及臆断的方式。我想我们都是多多少少接受过教育的人，应该给人于起码的尊重，最起码我们也应该能平心静气地讨论问题。我自己会努力做到这一点。<br />
<br />
　　知道你曾看过《转法轮》，原以为你会对法轮功有个全面的了解，看来你的认识还不够多，有些事认识的只是表面现象。而且你总喜欢用你对现代科学的认识来谈论法轮功，打个不那么恰当的比方，说一个人老用欧氏几何的观念去认识非欧几何，那是哪跟哪？有些事情我想我们也应该了解情况后再论对错。法轮大法加拿大网页(<a href="http://falundafa.ca/China/" target="_blank">http://falundafa.ca/China/</a>)上有一些关于最近发生在中国的情况的资料，欢迎你和任何其他有兴趣的读者阅读。这些材料虽说是法轮功弟子写的，我们也可以兼听则明。<br />
<br />
　　〖方舟子按：一、我主要地是根据李洪志本人对现代科学的认识来谈论法轮功的问题，指出李洪志对现代科学的无知。如果李洪志不把那些谎谬的认识栽到现代科学头上，而声称是他自己的认识，我未必会用现代科学知识来驳他这些认识。也打个不那么恰当的比方，如果李洪志老爱用欧氏几何的观念来验证自己的某个观念，我至少可以指出那些观点并不是欧氏几何的吧？可惜法轮功的信徒们并不理解这里的逻辑，他们却扯上了非欧几何来为李洪志对欧氏几何的错误认识辩护，这是哪跟哪啊？二、我们已刊登了不少法轮功的宣传材料和信徒来信。法轮功的网页敢登我那些批判文章吗？敢登其他人的批判文章吗？跟我扯什么“兼听则明”？〗<br />
<br />
　　看了一些其他学员给你的信，很受感动。他们绝大多数只是想让你和读者们对法轮功能有一个正确的认识，他们写的都是他们的心声，只是用他们的体会和经历来说明法轮功的好处。<br />
<br />
　　因为最近在赶一篇论文，不可能就你全部的观点和你探讨，这里只就想谈一谈邪教的问题。<br />
<br />
　　法轮功决不是邪教，这是肯定的。当然，我们得以理服人。首先，法轮功根本不是宗教。我们没有宗教形式，不收捐赠。一些人认为，信奉什么就是宗教了。这是不对的。试问，很多人认为科学是唯一正确的，那么科学也是宗教了。也有人说，你们在一起炼功不就是宗教吗？不是。我们在一起炼功只是相互促进。在一起干什么就是宗教，那么在一起复习功课也是宗教了。很多事情不能这样简单地去认识。我们在一起开会也只是学员间交流心得体会，要是这也能算作宗教形式，那么，全美经济学年会也是宗教形式了。<br />
<br />
　　〖方舟子按：一、信奉什么未必就是宗教，但把某个人当成神来崇拜绝对是宗教。二、我们不认为科学是唯一正确的。科学也有错，会出错，是一个根据新证据不断改正、完善自己的过程。你敢说法轮功有错，会出错？你敢怀疑李洪志的哪一句话是错误的？三、复习功课不是“崇拜”经书。对某人的话、某本著作顶礼膜拜、不可质疑、只能相信和理解的“学习”是宗教活动，就象基督教的查经班。三、如果全美经济年会是与会者集体学习、理解某一本书，不准对这本书质疑、批判，那么它就是宗教形式的〗<br />
<br />
　　其次，法轮功教人向善，使人道德回升，怎么会是邪教哪？有多少人修炼后家庭和睦了，有多少人修炼后在工作单位任劳任怨，有多少人修炼后在社会上成为好人，有些人修炼后戒烟、戒酒、戒赌，也有戒毒的。这方面的例子在《转法轮》中就有，还有更多的例子，在学员的心得体会中也比比皆是。我记得的比较感人的是以下一例。有个学员的工作单位要裁员，本来她不在下岗之列（法轮功学员在工作岗位都是尽心尽力的），可因为有个领导干部的亲戚要进来，就把她也列入了下岗名单。朋友们都劝她去活动，去闹事，可她因为不想给领导添麻烦，破坏单位的安定团结，默默地接受了这一切。我还记得有个单位负责分房的领导在分房分了一年多而毫无进展时感慨地说：“要是缺房户都是炼法轮功的就好办了！”因为他单位的法轮功弟子都不争房，都听从领导安排。法轮功的修炼者都严格要求自己，遇事先考虑别人，再考虑自己。法轮功学员离开中南海时，清除了所有的垃圾，包括警察和路人扔的烟头。不知你和读者们对此有何感想！在现今的中国，我说炼法轮功的人越多越好，对国家以至世界有百利而无一害。这么好的功法又怎么能是邪教哪？难道宣扬暴力、色情的反而是正教吗？<br />
<br />
　　〖方舟子按：一、戒赌戒酒不无理取闹之类本是基本社会规范，清除垃圾之类本是基本公德，有些人却需要迷信一个邪说才能做到，还当成什么大不了的事反复宣扬，真够可怜。而且，即使法轮功信徒有某些善行，也弥补不了法轮功在其他方面的邪恶，更证明不了法轮功的教义即为真。二、不无理取闹，但有理则该力争。法轮功宣扬有理也不要闹，是在宣扬忍辱负重、姑息养奸，放弃基本人权，与现代社会格格不入〗<br />
<br />
　　那么我们再来看一看你为什么认为法轮功是邪教。首先，我无法同意你对邪教所下的定义。按你的定义，基督教亦是邪教，因为它谈到了“世界末日”，说只有信主才能得到解脱。佛教也不例外，因为末法末劫的说法本来源于佛教。你又说，邪教是口头上教人向善，言下之意邪教是背地里教人干坏事的。众所周知，法轮功的学员到哪里都是个好人，我们就是从做好人做起，做更好的人，以至于更更好的人，这是有目共睹、有口皆碑的。如果说教人向善是宣扬邪教，那么世界上就没有正教了。<br />
<br />
　　〖方舟子按：主流佛教和基督教不宣扬世界末日“很快”就要到了。那些如此宣扬的“佛教”、“基督教”宗派都被公认为邪教。主流佛教和基督教的“末日”是一个无法确知的、遥远的日期，就象现代宇宙学的一大流派也认为宇宙在非常遥远的将来会毁灭一样。〗<br />
<br />
　　退一步讲，即使按你的定义，法轮功也不是邪教。你只注意到《转法轮》中多处提到劫难、末劫，便认为法轮功讲人类即将毁灭，所以是邪教。如果你看得仔细一些的话，你就该发现“末劫”是借用佛教的说法，劫难也是其它人提的。书中所说的只是一个理。李洪志老师也在不同场合讲过，过去(而不是他)所讲的劫难现在是不存在的。所以，即使按您的定义，法轮功也不是邪教。<br />
<br />
　　〖方舟子按：请看《法轮功五解剖》的举证、分析〗<br />
<br />
