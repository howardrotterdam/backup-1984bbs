<br />
“千人之诺诺，不如一士之谔谔。文王谔谔以昌，纣王默默以亡。”陈云林今天能说这话，也是我谔谔的结果。 本士大夫我再谔谔几句：别总想吓人家，如今大陆人你还吓不住呢，吓台湾人更不灵。 吓台就得下台。 <br />
