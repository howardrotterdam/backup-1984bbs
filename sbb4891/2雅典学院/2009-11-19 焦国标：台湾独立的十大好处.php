我写文章，向来不达到反人类的程度不动笔。 所谓反人类，就是与人类其他个体的观点相反。找个接近的表达，就是“为人性僻耽佳句，语不惊人死不休”。比如台湾独立，中国人个个诅咒之，今天我偏讲它有十大好处。比如台湾独立，中国人个个诅咒之，今天我偏讲它有十大好处。 <br />
<br />
第一个好处，台湾独立了，理论上讲一半以上的台湾人可以长出一口气，把悬几十年的心放下，睡个安稳觉了。为什么说理论上一半以上的台湾人？ 陈水扁当选总统，即意味著理论上讲一半以上的台湾人希望台湾独立。 <br />
<br />
第二个好处，大陆人从上到下也把“解放台湾”，或统一祖国的大包袱、大使命放下了。这个包袱熬煎了几代中共党魁，也把中国老百姓忽悠得猫三狗四儿的。 好象这祖国不统一，就不是中国种似的。<br />
第三个好处，台湾独立了，日本也放下了。台湾夹在中国和日本中间，台湾身分不定，日本也踏实不了。 台湾一独立，日本就踏实了。就象俩小伙子明争暗斗一个姑娘，最后姑娘落发出家了，或染重疾玉陨香消了，或宣誓终生独居，两人都死心了，好受了，踏实了。 <br />
<br />
第四个好处，美国也不再受道义上的责备了。政治制度上，本心上，美国亲台湾。 可是从实力角度考虑，又不敢过分偏袒台湾，惹大陆不高兴。所以只要台湾一天不独立，美国就得注定过双重人格、心理变态、心口不一的畸形生活。 <br />
<br />
第五个好处，北京共产党政权得救了。目前北京政权有三大克星，一是内政不修，一是日本觊觎台湾，一是美国保护台湾。 台湾独立了，中国便不再与日本、美国纠缠，起码台湾问题上不再纠缠，等于三大克星去掉俩。对北京而言，这叫退一步海阔天空。 <br />
第六个好处，独立后的民主台湾必将向大陆发射更强的民主电波，大陆民主化速度将加快。这与第五个好处不矛盾。 如果中国被日本、美国克死，是横死，将死得很难看。如果被台湾民主的电波电死，好歹还能落个囫抡尸首。 <br />
第七个好处，台湾远东孤儿的命运结束，可以作为正常的成人进入国际大家庭了。 03年闹萨斯，国际红十字会竟不把这个消息通知台湾，致使不少台湾人死于此疫流行。 红十字会也真够政治正确的，政治正确过度，就与杂种无疑。<br />
第八个好处，香港民主化将被激励。 港台呼应，可以更大胆地穿越专制北京制造的恐怖隧道，奔向民主的黎明。 <br />
<br />
第九个好处，解构了大一统的话语恐怖。解构一个少一个。 少一个话语恐怖，多一份言论自由。多一份言论自由，文明程度便向前跨进一步。 <br />
<br />
第十个好处，台湾独立了，东亚就安全了，国际社会的民主力量也加大了。<br />
<br />
台独这十大好处，也纸有我敢讲。为什么？ 因为我更爱国、爱生灵。不仅国台办不敢讲这话，便是胡、温都不敢讲。 为什么？因为他们是旧观念的奴隶，因为他们爱自己的权位超过爱一切生灵。 共产党眼下能把大陆一堆弄民主的搞压一压就谢天谢地了，别眼大肚子小，狗揽七八堆了。等将来需要，大家可以再结盟，就象现在欧洲一样。 <br />
<br />
