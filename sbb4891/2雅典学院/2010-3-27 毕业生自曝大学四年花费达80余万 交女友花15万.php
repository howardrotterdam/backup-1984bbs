																											<div id="postmessage_535079" class="t_msgfont"><a href="http://news.sohu.com/20100327/n271131535.shtml" target="_blank">http://news.sohu.com/20100327/n271131535.shtml</a><br />
<br />
84万余元，这是大学生李雷(化名)4年的消费。也许有人对这笔钱会感觉很惊讶，“咋花这么多呢？”可是，李雷却觉得“这很正常”。<br />
　　去年7月，家住吉林通化的李雷，在珠海某高校工商管理系2005级物流信息管理专业毕业，至今未找工作。今年3月24日，他来长修理心爱的台球杆，住在长春海航名门饭店。<br />
　　昨日，通过一位熟识的读者介绍，本报记者结识了李雷。在允诺不公布真实姓名的前提下，他开始统计他在大学4年的支出。其间，有些记不清的数字，他还通过电话询问他的母亲。两个多小时后，一份账单算出来了，他对此表示，在他的学校好友圈子里，这种消费水平只算中等。<br />
　　账单：<br />
　　花费84万余元<br />
　　路费：8.768万<br />
　　李雷家在吉林省梅河口市。<br />
　　从梅河口市到长春龙嘉国际机场，乘坐客车、出租车的费用为150元；从长春直飞广州，单程机票为2400元；乘坐大巴到珠海，车费90元；到达珠海市区后，乘坐出租车到学校价格为100元。<br />
　　按上述数字计算，李雷回家一次单程费用为2740元，往返5480元。<br />
　　“有时赶上家里这边有啥事儿，往返的次数会更多。”李雷回忆，“记得大二下学年期末考试时，正巧家乡有个朋友结婚，立马坐飞机回去了。”<br />
　　李雷称，包括寒暑假在内，每年至少回家4次。4年间，往返路费总计：87680元。<br />
　　电话费：4.56万<br />
　　李雷刚上大学时，不适应环境，经常打长途。他使用的是双项收费电话卡，大一、大二时，月均电话费约为1200元。大三、大四时，减少到月均700元左右。<br />
　　采访中，李雷用电话查询了话费：截至3月26日14时15分，本月消费523.24元。<br />
　　按此计算，4年电话费：4.56万元。<br />
　　电脑：2万元<br />
　　李雷经常玩网络游戏，在大学期间买过两台笔记本电脑，每台1万元左右。<br />
　　购置电脑花费：2万元。<br />
　　学费：6.28万<br />
　　学费每年是1.45万元，学校住宿费每年是1200元。李雷念了4年大学，学费、住宿费总计：6.28万元。<br />
　　爱好：4万<br />
　　李雷最大的业余爱好是打台球，他的装备是价格5000余元的台球杆。<br />
　　大学4年，每周他都要去珠海市区高档台球室打一次台球，有时聘请陪练一起玩，一次消费至少在200元左右。<br />
　　4年下来，李雷在打台球上的花费至少是：4万元。<br />
　　应酬：14.4万<br />
　　李雷介绍，大学期间，每周末，寝室同学要到校外聚会一次，包括吃饭、唱歌等娱乐项目，“AA制，每人每次大约300元。”<br />
　　“我每月有一半时间，晚餐都要与朋友到珠海市区比较不错的餐厅吃饭。晚饭后，还要去酒吧玩玩。”李雷说，先吃饭，后娱乐是他们一贯的消费套路。<br />
　　“我们经常一起出去聚会的朋友有四个，都是轮流请客，从不AA制。”李雷算账，每一顿饭，消费在300元左右。去酒吧消费，每次在1000元左右。<br />
　　初步统计之下，李雷说每个月在应酬上，至少花费3000元。<br />
　　按此标准计算，4年下来，总计：14.4万元。<br />
　　旅游：12万<br />
　　这几年，国内走过上海、香港、澳门等地，国外去过日本、瑞典等。李雷说，他每年至少要出去旅游4次，费用在3万元左右。<br />
　　4年下来，旅游共计：12万元。<br />
　　交女友：15万<br />
　　统计中，这是李雷最大的一块支出。<br />
　　“这一项算是我花的‘大头儿’。”李雷介绍，大学期间，一共交往3位女朋友，不管与其中哪一位相处，他都“不差钱”。<br />
　　“大三时，‘五一’黄金周，我跟女友去香港旅游。恰巧她过生日，就给她买了个一万多元钱的包。”李雷说，他找的女友都可称为美女。不过，他现在还是单身一人。<br />
　　4年，李雷交女友花费：15万元。<br />
　　摆事：1万<br />
　　李雷在大学期间，犯过一点小错误，为了摆平事端，顺利毕业，家人特来珠海花钱“摆事”。<br />
　　摆事费用：1万元。<br />
　　捐款：10元<br />
　　汶川大地震时，李雷正在韩国旅游。他回去后，学校学生会到他们寝室募捐。李雷说，当时兜里的零钱只有10元，捐了。<br />
　　4年大学，捐款总计：10元。<br />
　　学习用品：0元<br />
　　李雷坦言，这是最省钱的一块。大学4年里，他甚至没买过一支笔。考试时，为了涂卡，就把同学的铅笔掰开用。<br />
　　4年里，李雷用于自购学习用品的花费：0元。<br />
　　服饰：6万<br />
　　采访时，李雷穿在身上的服饰，大约3000元左右。<br />
　　“我的衣服很少有第二年再穿的，但我也不经常购物。我个人有个习惯，就是袜子、内裤更换后马上就扔，从不洗。我现在穿的袜子是98元买的。”李雷说，从小到大他从未洗过衣服。大学4年，估计扔掉的袜子有上百双。<br />
　　大学期间，每年购买服饰花费约1.5万元。4年共计：6万元。<br />
　　吸烟：3.358万<br />
　　李雷习惯抽国产某牌香烟，每盒23元，每天一盒，有时因为“发圈”，一天一盒也不够。<br />
　　4年下来，吸烟费用共计：33580元。<br />
　　手机：6.9万<br />
　　接受采访时，李雷身上带着两部手机。李雷回忆，4年里，总共换过23部手机，每部都在3000元至5000多元。<br />
　　李雷用旧的手机，除了送给别人，有些会卖掉，卖价是按原价1/4。由于常去聚会喝酒，酒后弄丢了5部手机。<br />
　　以每部手机3000元计算，4年购买23部手机的费用：6.9万元。<br />
　　综合计算，李雷大学4年里，14项费用支出共计：84万余元。而这是个不完全的统计数字，比如买背包的钱，就没有统计在内。<br />
　　李雷没干过一项工作，至今待业在家。他惟一一次收入，是在长春一家台球室打比赛，得到第一名，获奖金1000元。<br />
　　家庭：<br />
　　父母做生意<br />
