													<h2>不以言论治罪：宋朝的骄傲</h2>
						
						
																											<div id="postmessage_761194" class="t_msgfont">不以言论治罪：宋朝的骄傲<br />
<br />
现在有很多人，说到中国历史的耻辱就是宋朝。为什么呢？因为宋朝被少数民族欺负，被蒙古灭亡？其实在当时的世界历史，被蒙古灭亡的又何止宋朝。连最强大的阿拉伯(花喇子模等部)，波斯都被蒙古灭亡。而世界强国仅仅几个月就被灭亡，而宋朝抵抗了半个世纪。<br />
<br />
宋朝为什么值得国人骄傲？有一位外国著名学者说过，宋朝就是中国的文艺复兴。<br />
<br />
宋代科学技术无疑居于当时世界的最前列。两宋时代在科学技术方面所取得的成就之大、之高，在中国历史上是罕有的。震惊世界的三大发明――火药、活字印刷和指南针，就诞生于这一时期。正如国内外许多著名史家指出的，这三大发明改变了整个世界文明的发展进程。<br />
除此之外，中国人在许多方面也取得了成功，兴修水利，实施水稻的双季栽植；茶叶种植面积扩大；棉花成为普及性农作物；算盘开始应用，从此成为东亚商人的主要计算工具；火药的发明又推动了火箭、突火枪、火炮、地雷、火碌缺器的革命。总之，没有两宋社会良好的科技氛围，活字印刷的发明是很难降生的。可能活字印刷的发明是偶然的，但它确实又寓于两宋社会尊重科技、重视科技氛围的必然之中。<br />
在天文领域，宋代也取得了辉煌成就。北宋中期，杨忠辅制定《统天历》，以365。2425日为一年，这个数字与西方1582年颁布公历时的数据完全相同，比现代天文学所测数值只差26秒。由此可见，宋代天文学发展水平之高。<br />
<br />
在生物学和医学领域，宋代也是成就卓著。在经济文化全面发展的背景下，宋朝政府十分重视与广大民众卫生健康息息相关的医学事业，不仅政府出面编辑刻印了很多医学典籍，而且要求各州县加以推广应用。其中《太平圣惠方》100卷，收录中药处方16834个，《圣济录》200卷，是医学上的一部百科全书，收集了诊断、处方、审脉、用药、针灸等各方面的理论和实践成果。《政和本草》共介绍各种药材1558种，其中新增加628种新药，可以说是一部完备的药物学著作。南宋时期的宋慈收录并总结了前人的法医知识，创作出《洗冤集录》一书，其中涉及验伤、验尸、血型鉴定、死伤鉴别、检骨等多方面的理论和实践，同时对毒药和医治服毒的方法也进行了总结，这也是世界上最早的一部法医学著作。上述仅仅是介绍了宋代医学领域很小部分的成就而已，于此亦可以看出两宋时期科学技术发展水平之一斑了。<br />
<br />
除上所述之外，宋代另外两项重要成就有必要单独提一下:其一，宋代的绘画艺术。末代皇帝赵佶虽然做皇帝不行，但他对中国绘画艺术的巨大贡献却不可否认。首先，他是一位天赋极深的绘画爱好者，又是造诣很深的画家。其次，他又是宋代绘画领域的组织者和主要资助者。由于这一渊源，在宋代宫庭之内，集中了一大批杰出的画家。黄筌的花卉，李公麟的人物，米芾及子友仁的山水，徽宗本人的花鸟，皆卓绝于世。即使以现在的眼光审视他们的艺术水平，也可以说前无古人，至于是否有来者，则要看未来的艺术家是否能够超越前人了。其二，宋代还有一最辉煌的美术工艺，为历朝所不及者，就是磁器。功底深厚的宋代文化，统治者的自身的文化素养及价值取向，使凝聚在宋瓷上的文化艺术更是空前绝后的。以宋官窑代表典型的供御用瓷，其釉色平淡含蓄，素雅之中表现着内心的意蕴。从美学角度，它的艺术格调是高雅的，特别受到西方人的倾赖。<br />
<br />
细密的冰片，隐约间闪烁着钻石的光芒，仿佛是宋代艺人无意间冰镐敲击的严冰穿过时空来到了我们面前、温润而细腻。后世把宋代五大明窑称为“千古绝唱”。那是与历代仿制不成，差别巨大是有一定原因的。宋瓷是最精美艺术与精确工艺完美的结合。因为各地名窑瓷器大量地生产，不仅供皇家贵族使用，还为官员学者们、及市民阶级所珍爱、使用，所以让所有见着的人都赞叹不已。宋瓷许多的工艺都已失传，宋瓷许多的工艺水平即使现代都难达到。宋朝的瓷器是如此的精美，可以说宋朝遗留下的官办瓷器几乎都价值连城。自宋代以后，历代文人墨客及古玩爱好者对宋代官窑津津乐道，撰文著书者众多，但历史上能够亲眼目睹并真正鉴赏宋代官窑真面目的人少之又少。有关宋代官窑的论述变地扑朔迷离，则是仁者见仁，智者见智。这样，便给人们留下了发挥自由想像的空间。对宋官窑的研究也就成了中国古陶瓷学述研究中最为热门的课题之一。科技就不再多说，宋朝最让人惊叹的就是，不以言论治罪。<br />
<br />
宋朝的文人政治最彻底，文人出任国防部长（枢密院）是近代西方才出现的新鲜事，但在宋朝，这可是一个常例。军队的改革是军无常帅，帅无常军，这也是现代国家统帅军队的基本制度。据史载，太祖立国之后，曾在太庙里刻下祖训，此训只有皇帝本人在祭祀太庙时，由一个不识字的太监引导到太祖誓碑前背诵。开封城破之日，有好奇者跑到太庙时方知上面的内容（可以说是中国历史上最应不朽的名言）:“柴氏子孙有罪不得加刑，纵犯谋逆，止于狱中赐尽，不得市曹行戮，亦不得连坐支属”；“不得杀士大夫，及上书言事人”；“子孙有渝此誓者，天必殛之”。难能可贵的是，宋代历朝皇帝都还算听话。让太祖的这几条中国历史上迄今最为开明的政策，得到了切实的执行，达三百年之久，可以说这是宋朝三百多年的大宪章，也是同时代世界各国中最开明的大宪章，它从根本的制度上确保了宋朝所以成为中国文明的最高峰。<br />
<br />
宋代是真正的士大夫时代，也即是文人口中称道而身行之的时代。庙堂之上，君臣争论不已；江湖之中，书生指点江山。试问哪朝哪代，文人有这等身份和地位？朝堂之上，包拯吐沫横飞，仁宗皇帝不得不以绢试脸，而老包却只当不见，仍然在慷慨陈词；江湖之上，范仲淹妙笔生花:“先天下之忧而忧，后天下之乐而乐。”国学大师陈寅恪先生一段说宋的评价是:“华夏民族之文化，历数千载之演进，而造极于赵宋之世。后渐衰微，终必复振。”有宋一代，是中国知识分子活得最滋润的时期。也是中国知识分子政治上有理想、文化上有创新、道德上有追求、生活上有保障的社会。<br />
<br />
不可否认宋代有政治斗争，但政治斗争往往只是政见的不同，虽然有党同伐异，但没有从肉体上消灭。最多是贬谪。王安石、欧阳修、司马光、苏轼虽然政见上不同，但私谊却还不算。欧阳修死后，给予欧阳修评价最高的，不是他的党内同志，而是政敌王安石。可见那是一个君子时代。中国历史上有那么多的名人都出现仁宗朝（唐宋八大家有六大家在同时出现），绝非偶然，是适宜的政治政治环境孕育的果实。中国历史上的比较成熟的政党政治，就是这一时期典型的政治现象。以王安石为首的改革党和以司马光为首的保守党轮流执政近百年，这在世界历史上都是一个奇迹。<br />
这里需要指出的是，我这里提出的是政党，而不是历史上人们常常批评的朋党。中国历史有很多朝代，都存在大臣之间拉帮结伙，互相倾扎，为祸国家社稷的情况。欧阳修对此有过论述:“君子同道，小人同利”，历史上的朋党，往往纯粹为了利益而相互勾结，互为依托。政党则不一样，虽然他们之间存在利益，但重要的，是他们有理想、有诉求，并希望有机会实现这种道义。套用现在的话，就是政党首先必须有远大的理想，具体而现实的目标，有纲领、有组织、有领袖。这些现实时髦的东西，早在仁宗时代就实施了，虽然效果不好，最后未能挽救北宗的败亡，但其作为政治制度的先进性，却不能忽视，更不能抹煞。<br />
<br />
<br />
陈寅恪曾说“华夏民族之文化，历数千载之演进，造极于赵宋之世”.<br />
<br />
史学家汤因比（英国）曾说“如果让我选择，我愿意活在中国的宋朝”。<br />
<br />
连邓广铭也曾说“两宋时期的物质文明和精神文明所达到的高度，在整个封建社会历史时期之内，可以说是空前绝后的.<br />
<br />
旅美历史学者李亚平曾说：我最想生活在北宋的仁宗时代。<br />
<br />
易中天也说：宋朝是中国政治最文明，文化最繁荣的时代。</div>

							
							
							
							
