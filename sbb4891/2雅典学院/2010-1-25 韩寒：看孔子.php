													<h2>喜欢韩寒。特发一篇旧文顶贴。</h2>
						
						
																											<div id="postmessage_450385" class="t_msgfont">韩寒ＰＫ郑副主席：呼吸&#8226;呻吟&#8226;恼羞成怒（外一篇）<br />
<br />
楔子<br />
<br />
“一次我坐飞机，突然听见空姐对我后面的人说，‘主席你好，主席要喝点什么？’我吓了一跳，以为自己和胡哥买了一班飞机的票。后来想想不对，站起来特地看了看我上的是不是民航客机，往后偷偷一瞄，发现原来不是（胡主席）”。【注1】<br />
<br />
上述引号中的文字，除了里面的几个标点符号和文中用个“哥”字换下主席的名字以示亲切，以及最后括号中的胡主席三字是我画蛇添脚作了微调之外，全来自于80后文学青年韩寒的博客，是这篇名叫《领悟》博文中的一段。<br />
<br />
老实讲，读到此处时，我把刚喝进嘴里的茶，噗嗤一下喷到了我的电脑屏幕上，随即发出了旁人听来有点像呻吟的声音。<br />
<br />
韩寒讨人喜欢，我以为不仅仅是他的文章写得有趣和富有灵气，而是这位和我小孩年龄不相上下的小伙子，是当今中国一位不可多见的，既富有难得的良知，人格又比较正直和狷介的性情中人。<br />
<br />
今日，我就不拿这小子在汶川地震后深入震区从事志愿救援工作的事迹说事，以免别人说我这个无名之辈靠拍晚辈的马屁而扬名，而单单说说他拿中国的主席们开涮的事情。<br />
<br />
１<br />
<br />
时下的中国，是个协会泛滥，因而大小主席像蝗虫一样成灾的国度。其中，最令人感觉荒唐的是各级作家协会：主席一位，副主席十几位，加起来满满当当一大桌人。 这些人除了每月支薪和游山玩水的开销是件有数的事情之外，究竟都能写出什么好作品，确实是笔糊涂账。所以，当全国的几百位作协主席之中，有30多位最近拿出自己的得意之作到网路来晾晒后，在韩寒写出《领悟》一文之前，我这个一直以为中国的作协主席们除了领工资在行，而写作能力大多数已经退化到只能当应声虫的网路职业涂鸦者，竟然一点也没有感觉到。<br />
 <br />
韩寒在他的《领悟》一文中，首先否定了自己也准备拿出作品，和上述主席们的东西搁在一块晾晒和ＰＫ的传闻。还说，自己对“写东西没有多大的兴趣，不存在看别人写自己手痒的可能”。<br />
<br />
接着，韩寒话锋一转，开始拿主席们开涮。在说了和主席同机的有趣故事之后，他这样写道：“中国作协应该有百多个主席，平时赋闲，你突然给他们整个事，从活动筋骨和延年益体上来说都有很大的意义，而且事实证明，平时一直讲究领悟各种会议精神的老同志们还是不错的，平时看一期人民日报，就能领悟出什么该写什么不该写该怎么写，写到哪种程度。”<br />
<br />
估计会令所有作协主席产生掐死韩寒念头的恶意攻击话语，就是下面这段：“很快，就有人创作出了《从呼吸到呻吟》这样的文章。他已经领悟了网络上‘标题党’的 精神。标题党也是党，无论何时何地，都在领悟党的精神，这就是职业作家的职业风范。其实，现在是和平年代，才能容得一些其他作家蹦Q，作协还是厉害，这次 都没出动正主席，一旦中国再有个什么运动，他们才是真正的运动员和幸存者，别的三教九流肯定就歇了，因为你们不懂得领悟。”<br />
<br />
2<br />
<br />
果不其然。在韩寒的《领悟》像条毒蛇出笼之后的次日清晨，那位被韩寒戏称为“标题党”，小说《从呼吸到呻吟》的作者郑副主席，就怒不可遏地写出了《人不能信口雌黄》一文，搁在自己的博客上。<br />
<br />
这位中国作协圈子里被人称为“帅哥”的郑副主席，说着说着，就开始绕着弯子骂韩寒是个“有爹妈养，没爹妈教”的东西了。自然，郑虽然是个河南省的作协副主席，但毕竟骂人的话确实说的比较有技术含量，不像我这个没有写作执照的村野匹夫，讲的如此直白而已。<br />
<br />
郑副主席在文中是这样说的：“韩 说我们这些老作家都是靠领悟文件什么的才开始写作，未免太武断，不了解这一批作家不要紧，不要随便给他们下定义，一个轻浮到这种程度的人，肯定连他的父母 亲想什么做什么都不知道。当然，他的父母健在不健在，健康不健康我不了解，正因为我不了解，我不会说他的父母自（是）在哪种生活状态。”【注2】<br />
<br />
除了上面这段骂人家“有爹妈养，没爹妈教”的话比较不妥之外，郑副主席后面说的话，至少在我到看郑副主席写的《从呼吸到呻吟》小说之前，应该说并无不妥。<br />
<br />
郑副主席教导韩寒说：“大赛中没有作协主席，张笑天不是吗？起码看一看名单再说话，狂妄自大到连认真看一眼名单都没有的程度，就对这次大赛妄加评论，太自以为是了吧？”“韩寒根本没看我的作品就把我的小说定为标题党，未免太轻率吧。”<br />
<br />
看了郑副主席对韩寒的反批评，我顺便就搜索了一下《从呼吸到呻吟》究竟是写些什么东西。结果，马上就纠正了自己之前望题生义的印象。原来，这个小说不是写医生救病人的故事，也不是普及性知识的科普小说，而是写一位厂长和他那位“任何男人看了第一眼都想看第二眼的女秘书于晴”及众多工厂里的男男女女们之间发生的故事。难怪，在30多位晒作品的主席中，《从呼吸到呻吟》最吸引网民的眼球。据说，点击早已过了15万。<br />
<br />
3<br />
<br />
韩寒自然不是省油的灯。何况爹妈遭人作践，岂能善罢甘休。于是，隔日就以“标题党”当之无愧的青年首领的做派，用《副主席郑主席》【注3】为题，立马对郑副主席反唇相讥。<br />
<br />
不过，韩寒做得比主席有气度的是，首先把骂他“有爹妈养，没爹妈教”的《人不能信口雌黄》，全文晒在自己的文章之前。按法律术语来说，就是永远把证据固定在那里。考虑到韩寒有着中国作协任何一位主席都不具备的网路知名度，“副主席郑主席”立马人气飙升自然不在话下，关键是韩寒开始站在了中国所有主席的对立面了。<br />
<br />
在《副主席郑主席》中，韩寒汪洋恣肆地写道：“虽然郑先生是副主席，而且河南省有十二个副主席，但他在名单上是排在正主席之后的第一人，中国那点破规矩，不用干都能看会，所以，他将是现在河南省作协正主席退位以后的河南省作协正主席。我相信郑先生对正主席之位还是有想法的，你看他文章中的第二条就能隐约透露出他的想法。” <br />
<br />
不仅如此，在短短的１３００余字的短文中，韩寒居然金句频出：诸如“酒肉穿肠过，文件在你心”、“如果真的以作家论，你是要比我低级的，因为你是国家豢养 的”、“我们已经习惯了纳税人是孙子，公仆是爷”、“你明明就是郑主席，但却还是个副主席，这对不起你的工作，对不起你的名字”。<br />
<br />
估计会令郑副主席产生踩到一坨牛屎的极度郁闷感觉的是，韩寒居然开始对郑副主席进行人肉搜索，把这位主席的履历查了个底朝天。韩寒在文中是这样说的：“郑先生肯定记得，您在转业的时候，被河南省委组织部选为笔杆子，然后到了省委《党的生活》编辑部工作，当然当然，这是无尚的光荣。您还是灵宝市副市长，三门峡日报社总编辑、社长、党委书记，您说您不领悟，我实在不能领悟。”<br />
<br />
尤其令人捧腹的是，韩寒对《郑彦英诗语焦墨画――乡村模样》的调侃，简直会让人笑得岔气：其大意是，由于郑副主席领悟了中央建设和谐新农村部署的精神，所以，在郑副主席的“文章中，中国的农村无不是一副和谐的大好景象，男耕女织，衣食无忧，官民一心，繁荣富强。”<br />
<br />
最后，韩寒对郑副主席的挖苦，我估计所有中国的作协主席看了都会浑身不自在：“该写的写，不该写的你永远不会写，你领悟的很好。我看好你当选下届中国作协主席。”<br />
<br />
4<br />
<br />
说心里话，虽然韩寒不是，但我这人现在百分百是“有 爹妈养，没爹妈教”的衰人：爹妈全作了古，自己也退了休。所以，我现在不仅很喜欢看人吵架，尤其是喜欢看名人们吵架，而且更喜欢找人吵架，尤其是喜欢找那 些比自己更具知名度的人吵架，好搭车臭名远扬。当我看了一位年过知天命的知名作协主席和一位乳臭未干的网路最具人气的小孩吵架时，立时就两个眼珠里的瞳孔 扩大了将近一倍，高兴得简直有一种要死的感觉。<br />
<br />
我想，在这个所谓“话已说尽，事无可为”的崭新的和谐时代，犹如人在猪圈里，相互吵吵架，掐掐别人的软肋，骂骂别人的爹妈，只要不像杨佳一样白刀子进红刀子出，应该说既无伤大雅，也不会招惹有司，作为国人的精神生活调料，确实有助于和谐社会的建设。<br />
<br />
坦率地说，像我这种中国病人一样，对韩郑ＰＫ兴趣盎然的衰人绝对不在少数。按标题党主席小说的题目来说，他们全像我一样，立马由顺畅地呼吸开始变得唧唧歪歪地呻吟了。如若不信，我就便随手到韩郑二位的博客中找来证据。<br />
