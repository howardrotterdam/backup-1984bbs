我愿做“韩粉”们快乐的粉丝<br />
<br />
像我这种需要打发许多垃圾时间的衰人，看韩寒PK主席的游戏，且时不时地可以在没有什么流量的个人博客上涂鸦一大片文字而自愚自乐，不仅能够很快地打发掉那些不知道怎么用的垃圾时间，而且确实让我这个老顽童感到既好玩又开心。<br />
<br />
今早，我无所事事来到韩寒的博客，只见这个得理不饶人的小子，于凌晨3点多钟，当那位郑副主席正在席梦思上平和地呼吸，做着呻吟的美梦之时，却再次对那位拐着弯子骂他“有爹妈养，没爹妈教”的“副主席郑主席”更重地出手了。<br />
<br />
我真的不知道韩寒是否听了他哪位粉丝的建议，想开始“用保险杠和主席对话”。韩寒在这篇区区只有1500字，题为《驯化和孵化》的小文中，居然以这样的文字寒碜郑副主席：<br />
<br />
“老作家写文章讲究修改，老干部做事情讲究修正，所以在郑主席的博客里，文章已经有所删改了，由一开始的删评论也改为关闭评论了，因为观者甚，也适时加上了他自己写的文章的广告链接。其实这反而让人感觉心酸，有时想来，作为他们也身不由己，我想任何作家其实都不希望如此。”<br />
<br />
在寒碜了郑副主席之后，韩寒给出了他自己对作家的理解。考虑到我作为文抄公，经四川盆地里的混混宋石男当年免费做广告，早已臭名远扬，我也就不在乎再抄一回晚辈的文字了。韩寒是这样说的：<br />
<br />
“作家，其实在任何合理的社会里，哪怕和谐的社会里，都是让当局头疼的人，因为无论当局做的如何出色，必然会存在当局利益和人民利益冲突的地方，毕竟当局其实 就应该是物业公司，人民就是业主。如果作家们不用当局费心，甚至很讨当局欢心，那一定是有问题的。很多时候，作家是关心民生疾苦，鞭策监督当局以及驱动社 会公平进步的一个重要力量。虽然，你可以说，不是每个作家都想写这些，家各有志，我就是不关心世事，就是喜欢风花雪月饼，你奈我何。”<br />
<br />
写到这里，可能会有人以为，我也是个隐藏很深的“韩粉”吧？不，我亦忱除了喜欢抄别人的文章之外，从来就不做什么名人的粉丝，且无论这个名人的年龄大小。如 果谁要我非说出迷恋的人是谁，我会说，最迷恋的人除了父母之外，就是自己。对，你们完全可以说我是个自恋的中国病人，且自大加狂妄。<br />
<br />
但是，今天我开始想改主意了。我真的开始想迷恋那些跟在名人后面做粉丝的人了。<br />
<br />
这次，我看到韩寒在凌晨3点半把文章贴到博客上，只不过经历了短短1个多小时，他的粉丝就将评论写到了第4页。至下午3点半，也不过区区12个小时，其博文的点击就数直逼4万之数。在那些韩粉的评论中，我尽管老眼昏花，只翻到第4页，就眼冒金星，可还是发现了里面竟然不乏闪光的文字，而令我感到眼睛为之一亮。尤其是当我看到，那位和韩寒PK的郑副主席同样设在一个网站上的博客，其25万余的点击数，居然有20万是那一篇和韩寒过招，谈“信口雌黄”的东西带来，想想真是情何以堪，情何以堪啊！<br />
<br />
这也似乎从一个侧面证明，韩寒这小子下面说的话确实是有些道理的：<br />
<br />
“既然作协是一个驯化基地，已经有一大批人在那里，我们只当他们是会员就可以，不必当他们是作家，但一定要给他们的余生一个饭碗，毕竟没有功劳也有苦劳，就把 他们全都算作高级新华社社论员就好。或者像我以前说的，完全改革成妇联那样的组织，为作家寻求福利，打击盗版，联手抵制不法书商。”<br />
<br />
至于韩寒在文中说了一些在我看来极端反动的话，我由于鉴别能力很差，就留着愿意看的人自个去看吧。<br />
<br />
这回，我只稍微摘选十几个韩粉的留言，以表明自己作为韩粉们的粉丝，绝对不是言不由衷。<br />
