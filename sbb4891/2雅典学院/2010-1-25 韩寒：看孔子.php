													<h2>韩寒：看孔子</h2>
						
						
																											<div id="postmessage_449923" class="t_msgfont">今天我去看了《孔子》，在进场之前，我又离场了，是因为我要看清楚我究竟坐在哪里，免得一进去以后打扰到别人。一进场我就后悔了，电影院里不到十人，座位基本是自助的。在中国的古代，产生了一堆的子，虽然他们今天曰的和昨天曰的甚至还会自相矛盾，他们的意义不在于他们说的足够好，而在于他们说的足够多，各个时空的政客们各取所需，或推崇或批判。孔子就是里面最富有代表意义的一位。<br />
 <br />
说实话，我一直不觉得这些古代题材有什么拍成电影的必要，从电影的角度，类似的电影从诞生的一瞬间做的就是最反电影的事情，那就是扼杀想象力。但你要真说中国这些古代大片没有想象力吧，也不一定，编剧们经常创造出非常反历史的桥段，所以这本身是非常纠结的一件事情。之所以中国的大片大多数都要拍古代著名人物或者故事，源于制片方投资方没有什么安全感，他们觉得投了大钱，要完全建立在靠编剧导演创造出来的故事上不大靠谱，而偶然有导演同时拥有了巨大投资和自由创造的权利之后，拍出来的电影居然更加差。这一切，拧成了中国大片的悲剧史。至于周润发说，看完电影不哭的都不是人，我相信这是他的错觉，一定是在内部的看片会上，制片方都哭了。制片方哭的是，这得要拉多少的中小学生政府机关包场才能保本啊。<br />
 <br />
我们抛去一切政治的因素，从电影的本身看这部电影，这是一部失败的电影。电影里的说教毫无感染力，孔子一边在电影里说礼说仁的时候，我边上那人还用手机大声聊了十分钟的天，里面的战争如同儿戏，鲁国国力不济即将不保，而孔子的弟子只需要设一点路障向空中射箭就能退敌，而影片中，人和人之间的口交都没有任何的说服力，就好比小时候家长经常告诉我们，今日事要今日毕，但这句话其实不能说服你。这已经不是一个子多曰几下就能忽悠人的年代了。通过电影，你很难理解孔子这个团队里的员工为什么一直追随着孔子。在渲染人物性格的地方，我忍受了他们花十分钟让一碗马肉汤，来宣扬这个团队的凝聚力，因为我已经忍了孔子二十九代弟子孔融让一只梨的故事贯穿了我整个童年的时光。而任泉扮演的颜回最后居然为了多捞几卷文书，在冰水里冻死了，这不是感人，这是愚蠢。在前戏没有做够，过程乏善可陈的情况下，主人公就爽死了，这是不合逻辑的。在理想刻画的不够成功的情况下，这是毫无人性光环的桥段，放到现代电影里，就好比领导家被火烧了，领导的秘书冲进火海，救出了一本毛泽东文选，出来一看只救出了上卷，于是又冲了进去，又救出一本，出来一看妈的原来不是上下卷，自己救出的是一本中卷，于是明知道房子要塌了自己不行了，又冲了进去，然后房子就塌了他就死了。作为观众，你认为你能哭么。周迅的出场完全是因为制片方认为要加一个女人才能丰富电影，周迅扮演的南子对着孔子调情半天，但是子光曰不日，急死我了。最后南子的结局是不知道为什么被射死。<br />
 <br />
我给孔子打出了两分，首先，为了周润发之前在《阿郎的故事》中的精彩演出，鼓励这部电影一分，我想周润发的不哭不是人理论用在《阿郎的故事》中是成立的。最后，因为导演是女性，也鼓励一分。但是不得不说，无论是《孔子》或者另外一位女导演的《我是刘跃进》，这两位女性导演对于非情感类电影，尤其是比较庞杂的电影的把握能力都比较差，我不是特别理解他们为什么不去拍一些爱情电影或者生活电影，这是女导演的长项，类似张艾嘉的《心动》和许鞍华的《天水围的日与夜》都是女导演的好电影。女人何苦为难女人自己。<br />
 <br />
最后，我想说，《孔子》这部电影，无论从拍摄意义，商业利润，艺术追求，电影探索，教育启蒙，警世感人，视听震撼，娱乐消遣，记录历史等任何一个角度，都没有存在的必要，是一部完全可以抹去的电影。</div>

							
							
							
							
