&nbsp; &nbsp;&nbsp; &nbsp; ・我国楼市远离大众消费，成为了炒买炒卖的市场。<br />
&nbsp; &nbsp;&nbsp; &nbsp; ・广州一所八十余平方的普通住宅，因为与别墅在一小区内，被课以“豪宅税”。<br />
