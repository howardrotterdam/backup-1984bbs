&nbsp; &nbsp;&nbsp; &nbsp; ・安徽高速公路上卖石榴已成惯例，交警只罚买石榴的司机。<br />
&nbsp; &nbsp;&nbsp; &nbsp; ・中俄贸易需要扶持。俄封闭市场使中商几十亿商品损失后，北京俄商亦减少。<br />
