&nbsp; &nbsp;&nbsp; &nbsp; 求实，应该无所不在。比如，说非遗保护项目多，就得讲讲这六十年来，毁掉了多少传统文化的结晶。再看看这保护着的，又有多少不是为了发财。最典型的是中药，明摆着自古中药就不注射，干嘛现在要打针呢？而且打死了人还要打。这，还不是因为利于赚钱吗？<br />
