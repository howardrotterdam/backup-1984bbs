													<h2>接10楼</h2>
						
						
																											<div id="postmessage_841743" class="t_msgfont">依照马克思主义的历史理论，奴隶制社会-封建社会是一种进步。因为马克思主义当然要把社会进步看成是必然的，不然的话其理论就没煽动性了，也就是说：如果社会不是必然进步的，那么马克思主义的社会进步理论就不那么容易鼓动人类为社会进步而努力。<br />
这是一种基督教历史观，基督徒往往拒绝承认黑暗中世纪的全面退步，以此维护基督教对异教的优越感。当然，也是毫无道理的。<br />
至于把封建社会-资本主义社会看成是一种社会进步，无论这种封建社会指的是西方国家的封建社会，或者中国的”封建社会“（自秦至清），本身并非无理。只不过这种拿着”封建社会“当筐把资本主义前夕的社会都装进去的方法，符合马克思主义历史理论的粗陋性，或者说，只有粗陋的东西才容易得到大众的支持，才有煽动性。但也仅此而已。<br />
也就是说：这一错误的实质是缺乏精确性。<br />
因此，战胜这种错误，也不能靠指出其错误，指出其错误并不能使历史研究变得精确，必须找到一个合理的替代词汇，才能使历史研究变得精确，才能最终替代这种”封建社会“的概念。不然的话，既然如9楼所言，这种描述方面也并非没有一点合理性，留着也是没办法的事。<br />
