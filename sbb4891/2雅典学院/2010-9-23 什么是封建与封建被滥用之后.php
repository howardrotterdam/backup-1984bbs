																											<div id="postmessage_841573" class="t_msgfont">我真不知道，“封建”这个概念到底还要再滥用多久，国人才肯罢休，如果永远不能正本清源，那真是汉语的一大悲哀。<br />
冯天瑜先生在其新著《“封建”考论》①中，通过对中国、日本以及西欧有关“封建”定义以及封建社会特征的广泛深入考察而提出的中国西周至战国近于严格意义的“封建社会”、而从秦至清并非“封建社会”而是“宗法地主专制社会”的基本观点，不仅对于恰当分疏从西周至战国和从秦至清这两大历史时段的社会差异、准确把握这两大历史时段的社会性质、以及解决中西学术对话中因“封建”概念内涵的I格所造成的困境，具有相当重要的理论意义，而且对于矫正由于将自周秦以迄明清笼统纳入“封建社会”（或将魏晋之前归为更加低下的“奴隶社会”）所导致的思想史研究中的错误认识，也颇有裨益。<br />
自马克思的“五种生产方式说”传入中国、复经斯大林式的马克思主义将之规定为作为马克思主义哲学“两大板块”之一的历史唯物主义的组成部分、特别是在中华人民共和国成立以后这种历史观连同马克思主义一道被作为国家的指导思想的理论基础以来，为适应这一历史观，现当代许多历史工作者将所谓“奴隶社会”以后、“半封建半殖民地社会”（或可谓“中国特色的资本主义社会”）以前的历史时期一概纳入“封建社会”范畴。具体到中国历史来说，主要出现了“西周封建说”、“战国封建说”、“两汉封建说”和“魏晋封建说”四种类型。这四种类型说除了对于“奴隶社会”与“封建社会”分期的认识不同之外，基本上都认为中国自“奴隶社会”以后至“半封建半殖民地社会”以前的整个历史社会都是同质的，即是说，中国历史演进到“封建社会”之后，就没有发生质的变化，也不存在质的差异。由于这些历史工作者往往着眼于秦汉以降中央集权的专制政体来概括“封建社会”的性质，因此，“封建”和“专制”这两个本来相对的概念便被奇妙地组合成“封建专制”这一术语，甚至进而被简化为“专制”一词，用以指称早起西周、迟自魏晋以迄清末的中国古代社会。这样一来，对于这一中国古代社会的认识便被限定在这样一种格套之中：要么肯定自西周以迄清末都是“封建专制”社会，要么便不得不承认直至战国、秦汉甚至是魏晋之前都还处在比“封建社会”更为低下的“奴隶社会”之中。基于这种认识，那么，反映这一中国古代社会的主流思想，便要么是“封建专制”思想，要么甚至是“奴隶主阶级”的思想。自“五四”以迄1980年代所谓“新启蒙运动”对于这一中国古代社会主流思想的反复挞伐、全盘否定，基本上就是遵循着这一逻辑。<br />
冯先生关于“封建社会”的考辨，突破了上述历史观的笼罩，在“封建”与“专制”相对的本来意义上分疏了西周至战国和秦至清这两大历史时段的不同社会性质，从而使得分别形成或发展于这两大历史时段中、本来就蕴具不同性态的思想获得了社会存在方面的说明。以儒家思想来说，春秋战国时期形成并实现了第一期发展的原始儒家，在社会政治理论方面一贯表现出要求上下之间互尽义务、且特别强调在上者的率先示范作用、以及鼓励知识分子（士）以师友身份对当世统治者经常予以批评和抗议的德性政治精神。孔子所谓“君使臣以礼，臣事君以忠”②、“政者正也。子帅以正，孰敢不正”③、“（事君）勿欺也，而犯之”④；子思所谓“君子之道本诸身”、“君子笃恭而天下平”①、“恒称其君之恶者可谓忠臣”②；孟子所谓“君之视臣如手足，则臣视君如腹心；君之视臣如犬马，则臣视君如国人；君之视臣如土芥，则臣视君如寇雠”③、“惟大人为能格君心之非。君仁莫不仁，君义莫不义，君正莫不正。一正君而国定矣”、“责难于君谓之恭，陈善闭邪谓之敬，吾君不能谓之贼”④，乃至“君有过则谏，反复之而不听则去”⑤的政治主体意识、“君有大过则谏，反复之而不听则易位”⑥的革命精神、“民为贵，社稷次之，君为轻”⑦的民本观念，以及“居天下之广居，立天下之正位，行天下之大道；得志，与民由之，不得志，独行其道；富贵不能淫，贫贱不能移，威武不能屈”⑧的大丈夫气概和“说大人则藐之，勿视其巍巍然”⑨的特立独行人格，均明白无误地体现出这种精神特质。这种精神特质，当然不是秦汉以迄明清中央集权的专制社会所能孕生乃至容受的，而只能是统治权力相对分散，社会生活领域存在诸多活动空间，经济、政治乃至思想层面均有较大自由度的典型的封建社会的产物。正是有见于此，清人袁枚发论曰： <br />
赖有封建，然后栖栖皇皇，之卫、之齐、之陈蔡、之梁、之宋、之滕，几几乎有可行之势，而诸侯敬，子弟从，则声名愈大，千万年后，犹知遵奉为师。使圣人生于郡县之世，三试明经不第，则局促于一邦，姓氏列沉，亦遁世无闷已耳，安见其有以自立于天下耶？⑩ <br />
袁氏此论着眼于人才成就途径的区异而揭橥了“封建”与“郡县”（即专制集权）制度的差别，诚然不无见地。但是问题在于，“封建”与“专制”之不同，决不仅仅只在于社会一般成员的选择拥有多大的自由度和可能性，而主要在于权力架构是否存在着使社会一般成员得以保持独立人格和自主意志并在思想云为方面获得某种程度自由的空间。专制之为专制，正是由于它杜绝了或力图杜绝这种空间。在专制体制下，最高统治者通过层层向心的权力机构，最终以个人意志进行权力运作，以覆盖整个社会的权力网络，宰制着从中央到地方、从体制内到体制外一切臣民的生杀予夺大权，控制其思想云为，泯灭其自主意志，摧毁其独立人格。这才是秦汉以迄明清社会的基本性质，也是周秦之际发生所谓“古今一大变革”⑾或“天地一大变局”⑿的根本所在。由此，秦汉以降的正统思想才出现了颇不同于先秦时期的状况。仍以儒家思想为例。经过秦始皇的“焚书坑儒”以及汉高祖、窦太后等对于儒生的蔑视、羞辱和迫害，西汉承平以后便出现了制定朝仪的叔孙通、“称意任职”的嚎怼“曲学阿世”的公孙弘一类人物，从而开启了为着身家或小集团利益而与专制政体沆瀣一气、借用儒学资源诠释专制政体合法性的“政治化儒家”统系。笔者曾经细致比较过作为先秦文献的《郭店楚墓竹简・缁衣》、《上海博物馆藏战国楚竹书・缁衣》与经过汉代经师编定的《礼记・缁衣》的文本异同，发现后者对于前两种文本多有篡改，剔除了其中体现原始儒家德性政治精神的内容，而羼入了适应专制政体威权思想和纲纪观念的成分⒀，由此似可概见遭历秦火而复出、为汉儒重新编定的儒家经典及其表现的思想与原始儒家的本来面貌已有较大甚至是很大的差异，从而也反映出儒家学说随着社会性质的嬗替而发生了相应的蜕变。<br />
不过，实际中的思想总是既源于当下社会，同时又具有历史继承性。特别是那些浸淫于深厚传统而取精用弘、并具有深刻反思能力和批判意识以及“守死善道”精神的人物，往往更能超脱现实利害而从先哲思想中汲取道德智慧，从而形成不同于一般社会意识的、高标理想而抗衡当下的思想。具体到儒家来说，在发生于典型封建社会的原始儒家思想被阿附专制政体的“政治化儒家”扭曲为正统意识形态的同时，原始儒家的德性政治精神仍然被秦汉以降那些真正的儒者所继承并施之于立身行事。这些真儒总是（或最终是）处在体制的边缘或对立地位，成为无限威权及其衍生的黑暗腐朽势力的批评者、抗议者和反对者，其中许多人为此牺牲了身家性命。其荦荦大者如：秦末往归陈胜反抗暴政并因而死难的孔甲，汉初直斥窦太后而被罚入圈刺豕的辕固，汉武帝朝以“天人三策”制约君权的董仲舒，唐顺宗朝因挑战宦官和藩镇势力而被贬死的“二王刘柳”（王伞⑼跏逦摹⒘跤砦、柳宗元），唐宪宗朝因谏迎佛骨而贬瘴江的韩愈，南宋孝、光、宁三朝数以封事或面奏指斥君心之非而最终被打入“伪籍”、在“党禁”的喧嚣和阴冷中长逝的朱熹，明武宗朝几番与近习斗争而历蹈险境的王守仁，明清之际直斥“为天下之大害者君而已矣”并高倡“天下为主君为客”的民主原则的黄宗羲，提出君主苟不能“保类”、“卫群”则“可禅可继可革”的政治主张的王夫之，清初发为“自秦以来凡为帝王者皆贼”的惊世之论的唐甄，乃至东汉党锢、晚唐清流、明末东林，等等。这些秉具原始儒家德性政治精神的真儒的思想云为，岂可以所谓“封建专制思想”一语加以论定！实际上，在思想上与原始儒家德性政治精神一脉相承的历代真儒，恰恰是专制政体的制约、抗衡乃至否定力量，是与“政治化儒家”根本歧异的、抱持以道德理想转化现实社会政治这一儒家真精神的践行者，其思想实践非但不属于所谓“封建专制”的范畴，而且在创造性转化的前提下，恰恰可以作为当代中国民主政治建设中所尤为重要的培育民主意识的本有传统资源。<br />
关于原始儒家及其精神继承者在思想实践上与专制政体和“政治化儒家”的殊异，熊十力、徐复观、杜维明等现代新儒家几代人物早已多有指陈，其论概见于熊氏《韩非子评论》、《论六经》①，徐氏《两汉思想史》②，以及杜氏《道学政：论儒家知识分子》③、《杜维明学术专题访谈录：宗周哲学之精神与儒家文化之未来》④等著作中。实际上，不少研究者也都从孔子、思、孟以及周、张、程、朱、陆、王等历代儒家的著述中体认到了反专制的积极自由精神以及消极自由诉求。但由于受到长期以来形成的关于社会性质成说的局限，即使是服膺儒家德性政治精神的人们也往往因为难以解释这种精神的社会根源或思想根源而不敢对之加以充分肯定，一般人们更是溺于成说而习焉不察地目之为“封建专制”甚至是“奴隶主阶级”的意识形态。冯先生《“封建”考论》的面世，通过对“封建”古义的厘清以及对日本和西欧封建社会特征的考察，坐实了“封建”名相与西周至战国社会性质的对应关系，由此揭示了西周至战国封建社会与秦至清专制社会的相对性，从而为原始儒家及其一脉相承的思想谱系之反专制精神安立了社会的和思想的基础。因此，冯先生这部著作不仅对于中国古代社会性质的重辨，而且对于包括儒家思想在内的中国古代思想史的再认，都具有相当重要的启发意义。<br />
