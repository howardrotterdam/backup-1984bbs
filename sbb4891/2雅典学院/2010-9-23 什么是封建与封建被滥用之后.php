													<h2>什么是&quot;封建&quot;与&quot;封建&quot;被滥用之后</h2>
						
						
																											<div id="postmessage_841182" class="t_msgfont">看到很多网友分不清封建和专制，特转一篇文章来澄清啥才是封建。最好以后不要乱用了。<br />
------------------------------------------------------------------------<br />
<br />
<br />
什么是&quot;封建&quot;与&quot;封建&quot;被滥用之后 <br />
不穿裤子的云 发布于： <font color="#a5a5a5"><font size="2">2008-01-10 09:28</font></font><br />
&nbsp; &nbsp;&nbsp;&nbsp;“封建”这个词，在汉语中本有专属的含义，最初两个字单独使用，后来合用是在诗经中开始的，《诗经.商颂.殷武》：<br />
　　“天命降监，下民有严，不僭不滥，不敢怠遑，命于下国，封建厥福。”<br />
　　但通常认为这里的封建含义是“大立其福”之意，在“封邦建国”含义上使用“封建”一词，始于《左传.僖公二十四年》：<br />
　　“昔周公吊二叔之不咸，故封建亲戚以蕃屏周。…周之有懿德也，犹曰『莫如兄弟』，故封建之。其怀柔天下也，犹惧有外侮，I御侮者莫如亲亲，故以亲屏周。”<br />
　　　　《左传》全书，共有三个地方用到“封建”一词，两处即上述，还有一处是转引上述《诗经》的诗句。这里使用的封建，就是指封邦建国，捍卫周王室。<br />
　　　　先秦尤其是西周，封建一词的主体含义：<br />
　　　　“<strong>严格地说封建社会的要素是这样的，在一个王室的属下，有宝塔式的几级封君，每一个封君，虽然对于上级称臣，事实上是一个区域的世袭的统治者而兼地主；在这社会里，凡统治者皆是地主，凡地主皆是统治者，同时各级统治者属下的一切农民非农奴即佃客，他们不能私有或转卖所耕的土地</strong>。”（张荫麟：《中国史纲》，上海古籍出版社，2004年版，页27）<br />
　　　　<strong>自战国以后，郡县制开始出现，<font color="#ff0000">直到秦始皇统一六国，废封建而建郡县</font>，从此，中国历史上西周那种纯粹的封建制度就基本消失了。<br />
</strong>　　　　因此，在中国历史上，至少在民国之前，汉语中使用封建一词，是与郡县对举的，无论柳宗元著名的“封建论”，还是顾炎武杰出的“郡国利病”论，都是在与郡县制相对意义上谈论封建制的。<br />
　　　　1870年日本学者西周第一次使用“封建”一词对译英文“feudalism”，根据日本的历史特点，日本学者在使用该词时与其本国历史情形是比较吻合的。此后20年左右，严复先生在译介该词时，其含义保留了中国古意与西意，亦未出现语义的混乱。<br />
　　　　但是，新文化运动开始之后，在陈独秀的泛封建概念滥用下，这一概念开始“语乱中国”，他在1915年的《敬告青年》一文中，说道：“固有之伦理、法律、学术、礼俗，无一非封建制度之遗…”第一次提出了一个公式化的命题：<br />
　　　　“封建=君主专 制=20世纪前的2000年中国历史=落后”<br />
　　　　陈独秀之所以如此滥用封建概念，缘于其在日本留学，深切感受到日本的反封建热潮，而日本反封建反的是他们自己的封建，与中国何干？日本人并没有用错概念，而陈独秀却不管眉毛、胡子的差别，抓来就用，开启了中国人滥用封建概念的序幕。从此，以封建为前缀的词组作为批判性语词的运用方式就开始泛滥成灾：<br />
　　　　封建意识、封建专 制、封建地主、封建迷信、封建军阀、封建官僚、封建帝王、封建礼教、封建糟粕…<br />
　　　　反正一切不好的东西都可以加这样一个前缀，以至于这个看起来最活跃的词倒像个死东西，谁都不知道它是什么！<br />
　　　　民国年间，随着共产国际对中国政治的介入，他们滥用封建概念到中国现实的做法也影响了中国人，蔡和森就沿袭了斯大林对当时中国所谓半封建社会的论断，这时的陈独秀虽然是反对者，认为中国的封建社会早已崩溃，但他并未意识到自己所使用的封建概念依然是错误的。<br />
　　　　在滥用封建概念的诸种影响中，对史学全面的毒害则更多的来自郭沫若，他离开封建本义，完全自由发挥地使用此概念，然后将它机械地套用到中国数千年历史上，完成其“符合”“马克思主义”的五阶段说，这一流毒至今依然深种国人心中，尤其1949年以后，政治意识形态的介入则将封建一词的滥用在最大限度上推波助澜，便是今天的后果。<br />
　　　　然而，恰恰是马克思自己本人，提到过各种不同生产方式，从来不曾将中国归为封建制，在著名的1859年《政治经济学批判.序言》中，马克思说道：<br />
　　　　“大体说来，亚细亚的、古代的、封建的和现代资产阶级的生产方式可以看作是社会经济形态演进的几个时代。”（《马恩全集》第13卷，页9）<br />
　　　　这段话最清晰地表达了马克思没有把中国古代的生产方式看成封建制的。马克思与恩格斯关于亚细亚生产方式问题的通信中，讨论的思想因为涉及中国农业社会的公共工程，启发了魏特夫的灵感，以至于产生了一部曾经在中国引起轰动与争论的《东方专 制主义》。<br />
　　 在《资本论》第三卷中，马克思写道：<br />
　　　　“在印度和中国，小农业和家庭工业的统一形成了生产方式的广阔基础。此外，在印度还有建立在土地公有制基础上的村社的形式，这种村社在中国也是原始的形式。…在中国，那就更缓慢了，因为在这里直接的政治权力没有给予帮助。因农业和手工制造业的直接结合而造成的巨大的节约和时间的节省，在这里对大工业产品进行了最顽强的抵抗；因为在大工业产品的价格中，会加进大工业产品到处都要经历的流通过程的各种非生产费用。”（《马恩全集》第25卷上，页373）<br />
　　　　应该说，马克思所使用的封建概念是西方学术界通用的封建概念，正因为如此，这些经济学和社会学的学术问题上，马克思、恩格斯能够与其前后或者同时代的其他学者展开学术对话。<br />
　　　　英文feudalism，按照《大不列颠百科全书》第六卷的解释：<br />
　　　　“<strong>一种以土地占有权和人身关系为基础的关于权利和义务的社会制度。在这种制度中，封臣以领地的形式从领主手中获得土地。封臣要为领主尽一定的义务，并且必须向领主效忠。…在这样的社会里，那些完全履行正式义务的人，并不具有抽象的国家或公务的概念，而是由于同他们的领主有私人的和自愿的联系，接受以领地形式给予的报酬，这些领地可以世袭</strong>。…”（中文版，中国大百科全书出版社，2002年版，页280）<br />
　　　　可以说这是一个最有权威性的概念表述。<br />
　　　　自马克思之后，年鉴学派重镇马克.布洛赫出版了著名的《封建社会》上下卷，对封建的广狭两意，及其具体历史形态都作了精详地阐述与论证，另一位年鉴学派大师布罗代尔在《15―18世纪的物质、经济与文明》也涉及到了这个问题。<br />
　　　　另一方面，当代左派学者安里.佩德森也对亚细亚生产方式问题进行了很好的全面论述，在其名作《绝对主义国家的系谱》一书中，他认为马克思关于中国的看法和判断缺乏实证的研究，这是很到位的，不过他讽刺马克思攻击太平天国，倒显出其自身的浅薄和对中国历史的隔膜，此处不赘。<br />
　　　　不过，对世界各种封建生产方式的诸形态之考辨，以及对中国的独特性的分析最为到位的，我以为该首推马克斯.韦伯，他在研究世界各国历史之后，将广义的封建关系划分为下列三类：<br />
　　　　1、“赋役式”封建制（例如哥萨克骑兵）；<br />
　　　　2、“家产式”封建制，细分为：<br />
　　　　A、“庄园领主”封建制，部曲军（例如罗马贵族直至内战时期仍习惯利用此种军队；此外尚可见之于古埃及法老军队）；<br />
　　　　B、“人身领主”封建制，奴军（古巴比伦与埃及的军队，中世纪时阿拉伯的私人军队）；<br />
　　　　C、氏族封建制，充当私人军队的、世袭性的客（罗马的贵族）；<br />
　　　　3、“自由的”的封建制，细分为：<br />
　　　　A、“扈从式”封建制（大部分日本的武士，梅洛林王朝的侍卫）；<br />
　　　　B、“俸禄式”封建制，仅基于庄园领主权与征税权之授予，而非个人性的效忠关系（近东诸国，包括土耳其的采邑制）；<br />
　　　　C、“采邑”封建制，基于个人性忠诚关系与采邑制的结合（西欧）；<br />
　　　　D、“城市支配”的封建制（例如斯巴达类型的希腊城邦）<br />
　　　　（上述见于《韦伯作品集.卷三.支配社会学》，康乐、简惠美译，广西师大出版社2004年第一版，页199―200）<br />
　　　　即使是在广义的封建概念中，韦伯也没有将中国秦以后的古典时代列入，他说：<br />
　　　　“西元前9到3世纪之间，实际上是一个各个独立的封建王国并存的时代。”（《韦伯作品集.卷五.中国的宗教》，康乐、简惠美译，广西师大出版社2004年第一版，页80）<br />
　　　　对于“这意味着封建制度的全面废除”（同上，页77，指秦）之后中国的经济生产方式，韦伯有另外一个著名的概念，是专属于分析秦以后的中国古代社会的，即“家产官僚制”。韦伯就与此相关的一些论断，是极为精到的，例如，他说：<br />
　　　　“土地所有权极度不稳定的性格，明显地贯穿于长远的世纪，亦即，有一千五百多年之久。”（同上，页131，他不知道在他1920年去世之后100年，中国的土地所有权能稳定便是拜天所赐了。）<br />
　　　　“社会平均化的倾向是与家产官僚制相呼应的。…土地的分割继承，最后则大大加强了土地所有的民主化，虽然在个别情况下，由于共同继承的缘故，此一趋势有所减缓。”（同上，页136，关于平等性这一点，在中国史学界也多有人涉足，但很少是从土地制度入手的，更多是从科举制入手，这充分显示了韦伯惊人的洞察力。）<br />
　　　　“社会秩序里具有封建成分的身份性性格，至少在法律上被消除了。…名门望族的‘乡绅’，并未享受到国家保证（相对于下层社会）的地位。按照法律，直接位于小市民与小农民之上的，是家产官僚制的机制。西方中世纪时那种封建的中介阶层，无论在法律上，还是实际上，都不存在。”（同上，页136）<br />
　　　　可以想见，通过这些分析，韦伯的结论，无论是与黑格尔的论断，还是马克思、恩格斯的论断，都存在交叉部分，即中国古代自秦以后，与西欧类似的封建制是不存在的。这里有个极为重要的必要条件，就是法权上对所有权和某些与个人自由相关的特权保护，在制度意义上，中国古典社会是不存在的。正是这种制度性的匮乏，导致了中国历代王朝末世官僚体制对人民的严重压迫，而这与皇帝优劣关系密切，然而当一个国家总是寄望于某个人的时候，她就是危险的。<br />
　　　　无论如何，没有“风能进，雨能进，国王的军队不能进。”的法权保护，西欧式的封建何以在中国存在？<br />
　　　　由于民国以来，尤其是1949年以后，“封建”这一概念无论在学术意义上还是一般社会用语意义上极度混乱的使用，导致了我们对历史认识的严重混乱。直到今天，它还大量地存在于人们的观念以及学校教育之中，即使在大学，也没有多少教师能够有效地避免这一概念的混乱使用。五年前，我在应某著名杂志约稿所写的《走向公民时代》一文中，谈及中国古代社会均以“古典时代”名之，而编辑在修改时，径改为“封建社会”，而这种谬种流传的无奈状态还不仅仅是这一家媒体，这表明即使作者自己力图纠正语言错乱，而媒体却常因为无知也会将混乱进行到底。<br />
　　　　对封建概念的滥用导致了几大恶果：<br />
　　　　1、无法把握中国历史本身的独特性，机械地套用导致的非驴非马历史观，相信马克思在世也定会十分厌恶（他在给俄国人回信中就表达过这种缘于栽赃的愤怒）；<br />
　　　　2、无法清晰比较中西方的差异性，将各种完全不同的历史混为一谈，例如，对西欧、对日本与中国历史的差异；<br />
　　　　3、窜乱了汉语原本可能的准确表达，使得全社会在混乱的语言无意识中莫名其妙地表达、不知所云地表达；<br />
　　　　4、严重破坏学术交流的基本规则，当一个概念歧义纷纭，甚至在几乎相反含义上同时使用的时候，学术交流就会彻底不可能，像郭沫若那样完全在本意之外滥用封建概念的时候――例如“封建专 制”（无论在马克思那里还是在其他社会学家那里，封建与专 制是一组对立的词，而一旦两者结合在一起，岂不就是说“我饿得很饱”这种呓语？）<br />
　　　　……<br />
　　　　除了“封建”这个概念之外，还有无数的概念，都被长期滥用，好在对包括“封建”在内的一系列概念，当代学界早在15年前就开始了严肃的驳正工作，其中冯天瑜教授的贡献最大，本文的词义考证资料大部分来源于他的杰出著作《“封建”考论》。但正如大学者唐逸先生所言，美丽的汉语变得如此混乱，真是令人痛心之至。<br />
　　　　我真不知道，“封建”这个概念到底还要再滥用多久，国人才肯罢休，如果永远不能正本清源，那真是汉语的一大悲哀。<br />
