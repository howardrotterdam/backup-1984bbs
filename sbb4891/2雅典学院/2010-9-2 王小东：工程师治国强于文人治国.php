 <br />
工程师治国强于文人治国<br />
作者：王小东<br />
来源：《绿叶》 2010年第7期<br />
来源日期：2010-8-10<br />
　　<br />
&nbsp; &nbsp; 西方发达国家最上层的治国精英，多数有文科背景：法国多为高等行政学院毕业，日本多出自东京大学的文科，美国则多有法律专业背景。反观当代中国上层政治精英，则多为工科出身。现在中国是不是应该像西方那样，更多地由文科出身的知识精英来担当治国大任呢?<br />
<br />
　　我认为这样不合适。主要理由：第一，西方上层的文科精英并非单纯的“文人”，他们同时对科技、逻辑比较精通。第二，有技术者没文化、有文化者没技术，常被视作中国知识精英的通病，但是比较而言，中国技术精英的水平还是要高于人文精英的。下面详细阐述一下。<br />
<br />
　　一、西方知识精英结合文化与技术的过程<br />
<br />
　　1.从奴隶到科学家与工程师：西方科技知识分子的身份演化<br />
<br />
　　在人类历史上，不仅中国文明，而且古希腊古罗马代表的西方古文明，人文知识分子一直占有很高的地位。过去所谓的学者、读书人、知识分子，仅仅指的是“文人”。根本不包括掌握技术的工匠。其实，人类历史的进步，技术推动功不可没，掌握和改进技术的工匠对于人类的贡献事实上是极大的。像人类文明划分为石器时代、铜器时代、铁器时代，都是以材料技术来命名的。新石器时代和旧石器时代的差别尽管不是以材料，也是以工艺来划分的。但是，历史上工匠的地位一直很低，中国如此，西方更是如此，工匠不过是奴隶。<br />
<br />
　　柏拉图看到他的有些哲学界的朋友搞工匠技艺研究一一在今天都是理工工程师干的事儿一一的时候很生气，他说这是奴隶干的事儿。亚里士多德讲过：“在治理得最好的城邦中……市民们既不从事手工业，也不经商，因为这种生活缺乏高贵的色彩，与美好的名声背道而驰。”罗马哲学家塞内加在给卢齐利乌斯的一封信中则认为：“众所周知，有些东西仅仅出现在我们的记忆中。如玻璃窗的使用，它能让充足的阳光透过一块透明的玻璃照进房间;浴池下层结构及其嵌在墙上的管道，能发出热量，使池内上下水温相等。……还有速记，它能记下最快的演讲，手舌并驾齐驱。但所有这些都是最卑贱的奴隶发明的。哲学则处于更高的地位，她并不训练人的手，而是精神的导师。……是的，我说，她决不是一个仅仅为生产日常必需品而制造工具的工匠。”<br />
<br />
　　古代的知识分子是非常优秀的，他要去搞技术的话肯定也会同样出色，但是他不去，因为那样他就跟工匠没有区别了，有损自己的高贵身份。这一状况，到了近代在欧洲发生了重大转折，知识分子中的相当一部分人，而且往往是非常优秀的那一部分人，开始介入科学技术，参加到了传统工匠的行列中一一这实际上可以被看作是前现代化社会和现代化社会的一个分界线。这首先极大地促进了科学技术的发展，成为我们今天所享受的具有种种便利的现代化生活的支柱。其次，工匠的地位得到了极大的提升，他们被尊称为科学家与工程师，成为知识分子的一部分。<br />
<br />
　　对此演变，斯塔夫里阿诺斯在《全球通史》中概括得非常好：“正是这种哲学家与工匠相分离，阻止了欧亚大陆诸文明的技术发展，也正是哲学家的有条理的思维与工匠的实践经验及传统知识这两者间的相互影响，使得西方在近代完成了伟大的科学和工业革命，而为人类的发展出了杰出的贡献。”<br />
<br />
　　在这一过程中，西方知识精英在文化与技术的结合方面做得比较好，文化与技术间割裂的弊病，在他们身上的表现相对不太严重。其根源我们就不详加探究了，总之这是事实。<br />
<br />
　　2.中国尊崇文人的传统<br />
<br />
　　相较于西方，对知识分子的尊崇在中国文明中可能更甚。中国有着非常优秀的养士传统，把他们的地位抬得很高，给予他们很多的特权，有所谓“皇帝与读书人共天下”的说法。而读书人当中虽有败类，但作为一个阶层，确实起到了社会栋梁的作用。中华文明历经几千年而不衰，读书人在政治、社会结构中所占据的位置，不能不说是一个原因。<br />
<br />
　　但是这一传统也存在着严重的弊端，那就是过度地重视了一些纯文艺腔的东西，比如说诗词歌赋等等，这些东西与经邦治国富民并没有直接关系。诗词歌赋这样的东西，甚至在某种程度上说包括一部分的文史哲，实际上是高度发达的文明的一种奢侈品。古希腊、古罗马有资格享受或供养这些奢侈品，在那之后，西方陷入中世纪黑暗时期，文人地位也就降低了，直到文艺复兴。中国古代文明维持的时间最长，拥有这种奢侈品数量最多，时间最长的自然也是中国。即使在古代中国其实也不是没有人认识到这一点，王安石变法时，就想在科举考试中取消诗词歌赋这一科。这种想法绝对不是嫉贤妒能、心理阴暗，因为王安石本人就是诗文大师。他这样做说明，古人当时已经认识到诗词歌赋这东西太虚，它跟治理国家、管理国计民生这些实事是有距离的，国家还是应该优先考虑点更务实的东西。<br />
<br />
　　像西方知识分子那样介入科技领域，在中国也不是没有，只是太少。像中国人喝茶爱用的紫砂壶，其中好的款式几乎全是创自清代，因为这是由一些知识分子来设计的。这也再次说明，素质高的人文知识分子要介入科技领域的话，也会是非常优秀的。但今天中国的人文知识分子，从总体上说是素质不高的。<br />
<br />
　　3.工科知识分子在西方的地位被中国人低估了<br />
<br />
　　当代西方理工科知识分子的地位常常被我们低估了。现代国家大都认识到理工科特别重要，西方大学80%是文科，但是他们普遍认为权力是掌握在20%的理工科知识分子手里的，当然还有一部分权力掌握在政经法这些硬文科手里。即使最上层的治国者，在西方也有很多是理工科出身的，而并非像大家通常认为的那样，全是硬文科的。像日本新当选的首相菅直人，是学应用物理的。当代西方最著名的两个女政治家，撒切尔本科是学化学的，默克尔则是搞物理的。这类事实应该引起我们的重视。<br />
<br />
　　我上世纪八九十年代在加拿大留学，有白人大学生在学生刊物上发表文章，说白人的地位岌岌可危，因为白人学文科的太多，亚洲人都学习理工科，而任何一种权力，技术、经济包括政治权力多数掌握在理工科手里，所以将来白人的地位会被亚洲人挤占。可为什么那么多白人去学习文科呢?理其实很简单，文科好玩儿，有钱了就不用再学理工科了，这太辛苦。不只是加拿大，这在西方是普遍的。白人富家子弟，就是说用不着再挣钱的人，用不着为生计奔忙的人，他们最想学的就是法国文学，好玩儿啊。按中国一个年轻人的说法就是泡妞很容易，理工科整天学习，没情趣，不浪漫，不招女性喜欢。这样产生的问题是不挣钱、没权力，可是因为他们的父辈已经掌握了权力，有了钱，所以他们就不用在乎了。玩儿的结果是会逐渐丧失权力和财产，这正是那些有志的白人学生所担心的，因此希望更多的白人学生来学理工科。白人统治这个世界不是靠法国文学，而是靠坚船利炮、卫星导弹，没有了这个就什么都没有了，而这些东西都是要依靠科技的。<br />
<br />
　　近代以来西方过舒服日子已经很长了。舒服日子过久了.必然会出现中国古代出现的那种情况.文人知识分子缺乏足够的生存压力，乐衷于玩弄词藻，占据道德制高点。因为这个社会好像不需要他们发挥什么真实的作用，大家就都玩去吧。问题是现在西方有条件玩，中国没条件，尽管在历史上很长一段时间中国是曾经有条件玩的。<br />
<br />
　　二、科技精英治国的历史合理性<br />
<br />
　　1.当代中国最优秀的知识分子多集中于理工领域<br />
<br />
　　中国到了近代之后，被西方的坚船利炮打败了，被迫地走上了科技和工业化的道路，知识分子不能再一味地图享受当文人了，开始大量地进入理工科领域。尤其是1949年之后，中国理工科知识分子的队伍在数量上和质量上都在迅速提高。我上世纪八十年代到日本去留学，才发现理工科和文科的比例，中国80%对20%，而日本则是反过来的。一般来说，国外大都是80%文科，20%理科。苏联我搞不清，可能跟中国差不多，因为中国当时学苏联，一心想赶超。<br />
<br />
　　这个比例对中国这样一个赶超当中的国家来说是合理的。中国首先要解决的任务是不再挨打，那就得有好的武器，那就离不开大量科技人才。什么制度层面、文化层面，先解决器物问题才是最重要的，有时候器物的改变才决定了制度、文化的改变。<br />
<br />
　　为了赶超，优秀人才全被理工科吸走了，理工科不管在学生总量还是尖子总量上都远远超过了文科。不单共产党，国民党也是有现代化倾向的，蒋介石提的目标也不是要多少文人，而是要培养多少个工程师，只是没有中共后来那样集中。毛泽东在文革期间说“大学还是要办的，我这里主要说的是理工科大学还要办”。这种社会氛围和导向致使最优秀的人才集中在了理工科。即使毛时代结束后，到了我们上大学那个年代即上世纪八十年代，文科最优秀的学生的考分恐怕连二三流理工科学校都进不去，当时考文科的人很可能就是因为考不上理工科，这是实话。<br />
<br />
　　其次，还有政治方面的原因，也使文科难以吸引到优秀人才。49年以后，搞文科危险，容易在政治上出问题，这导致本来想去或适合去文科的优秀人才，也权衡利弊转投理工科了。即使到了八十年代，这种顾虑人们也没有完全打消。以我本人为例，我原本是很喜欢，也很适合文科的，但是八十年代初期高考的时候，家里坚决反对我报考文科，理由是怕我出政治危险。我就考了理工科，本科毕业后才再转到文科。<br />
<br />
　　因此说，1949年后.相比于科技知识分子，中国文科知识分子一一不管是文史哲软文科还是政经法硬文科一一的素质是比较差的。过去有句话讲“学会数理化，走遍天下都不怕”，没有说学会文史哲政经法走遍天下都不怕的。由此可见，在近几十年的中国，由工程师、由理工科人才而不是文人来治国，是有其历史合理性和正当性的，毕竟他们是从中国最优秀的人才当中选拔出来的。<br />
<br />
　　2.科技精英治国的缺陷<br />
<br />
　　理工科治理国家的缺陷也是显而易见的，那就是他们容易缺乏全局观念，而这对治理国家又是至关重要的。<br />
<br />
　　理工专业讲的不是治理国家、管理企业这种事情。治理国家、管理企业，要更多考虑到人的问题，而理工科考虑人与入之间的事情会少一点，更注重人与物之间的关系。人与人的关系。人与人的世界非常复杂，不可能像理工专业当中的对和错那样无可争议。比如数学里边的对和错就很少有争议。工程技术的对和错也比较容易判定，比如说你盖个房子塌了，就错了，房子没塌，就对了。但是人和人之间的关系就复杂多了，有的时候对和错很难判断。我是从理工科转向文科的，一开始我就不适应，我原来认为对和错应该是很明确的，怎么到这儿全都不讲理了呢?我就感觉文科不讲理，跟你胡来，心理承受的压力非常大。直到真正上了一个层次以后才明白，这其实是人类社会的常态，你得适应这个世界。<br />
<br />
　　这一缺陷又引发了新的缺陷。既然文科处理的问题可比理工科复杂得多了，那么理工科的人就有处理、应对不了的时候，就会产生强烈的挫折感，特别是他看到这个问题比他原来的专业领域要复杂得多的时候，他的挫折感还是很大的。于是有人就失去了自信，又跳到另外一个极端，就是很惧怕、很崇拜所谓的人文知识分子。没有文化的人，以没有文化为荣肯定不好，但是，因自己没有文化就崇拜所谓的“文化”一一也许是个假文化，也不知道这文化是什么，就迷信它，这更不好!<br />
<br />
　　三、当代中国文人的弊病<br />
<br />
　　1.当代人文知识分子的水平依旧相对较低<br />
<br />
　　毫无疑问，当代中国文人的不足首先在于，前三十年甚至更长时间内中国最优秀的人才一直集中在理工科，而不是文科。那么这种情况在今天得到根本改变了吗?我认为还没有。最近这些年，人文知识分子水平没有提高多少，地位或影响力相较于过去，倒是有了不少提高的，主要原因如下。<br />
<br />
　　首先是因为中国传统文化中对文人以及人文高度敬仰的传统还继续存在。即使是毛时代，表面上看不重视文人，一是把尖子都弄到理工科了，二是整了很多文人。但另一方面，他为什么整了这么多文人?这恰恰说明他是很重视这些文人的，老是怕他们会发挥什么大的影响，颠覆了政权。当年李希凡、蓝翎写了一篇评论《红楼梦》的文章，就能得到那么高的重视，这种事情在其他时代恐怕是很难见到的。<br />
<br />
　　文革结束之后，包括尊崇文人在内的诸多传统迅速反弹。比如，像林徽因、徐志摩那点浪漫故事被反复地传唱，最近又在宣扬什么吴宓和毛彦文的爱情，甚至在党内，也得弄一个高君宇和石评梅出来。他们对于中国历史的影响真的是那么大吗?事实上有无数的工程师和科学家，无数的军人、政治家，比他们所起的作用大多了。这些文人的东西被放大，其实是中国传统文化的一部分，现在还是这样。<br />
<br />
　　其次，实行市场经济之后，最起码文科中的商、经、法有经济效益了，甚至金融专业能够发大财，而原来经济效益是理工科才有的。经管法律金融这些专业，尤其是金融，开始吸引大批人才，学生素质现在是改善了。<br />
<br />
　　但是人文知识分子水平偏低的状况依旧未能扭转。水平的高低，除了学生，还得看老师。现在文科老师的素质太差，基本上还是90年代以前培养出来的，其水平和素质跟理工科有较大的差距。即使现在文科的生源比过去好了，跟着这样的教授，水平也高不到哪里去。因此，文科学生水平的真正提高，得有耐心。<br />
<br />
　　再者，大学文科课程太轻松了。文科很多学生都清清楚楚地知道他不用学习，甚至连复习都不用，稍微在考试之前翻一两天的书就能过。理工科是这样的吗?最起码一个像样点的、正经点的大学的理工科不会这样容易过关，还是需要努力学习的。凭此就能判断出许多文科生根本没学到什么东西，一玩就是四年，里面的好苗子，反倒给糟蹋了不少。<br />
<br />
　　2.“文艺腔”：文人缺陷的具体表现<br />
<br />
　　文科学生或文科知识分子水平低，具体表现在什么地方?第一，自然科学知识极差;第二，逻辑思维能力极差。跟国外的文科相比，中国的这种差距非常之大。既缺乏自然科学知识，又不讲逻辑。那么具体到处理问题的时候，文人们就只会讲漂亮话，玩弄词藻，只<br />
<br />
　　知道抢占道德制高点。但是真正有用的，比如在讲治国，甚至是管理企业这些方面，他们其实是没什么本事的。这种特征被称为“文艺腔”。<br />
<br />
　　将来的理想状态还是应该文人治国，但绝不是现在这样的文人。理想的文人，不要求他在自然科学方面能做专业性的工作，但是起码应该了解自然科学的思维方法，它的最新发展。比如由你来给自然科学研究拨款，你要不懂的话，怎么知道该给谁不该给谁啊?再一个就是要具备逻辑思维能力或者说有条理的思维能力。尽管中国古代没有逻辑这个概念，但最起码王安石明白，一个词藻漂亮的人跟一个懂道理的人还是有差别的。他做改革，想选拔的是懂道理的人，而不仅仅是一个词藻华丽的人。治国，最起码在讲道理和懂道理这方面，你得具备非常高的素质。<br />
<br />
　　现在许多文人，甚至连漂亮的词藻都不会玩了，只剩下唱高调，其他的都不管，置世界的复杂性子不顾。比如有人爱讲“战争泯灭人性”。确实如此，但是进一步讲，你不准备战争你会被别人消灭掉，这样的东西他就不讲了!环保领域也是如此。环保既是个技术问题，又是个政治问题、经济问题，甚至还是个军事问题。环保领域搞科技的人可能对他的专业有所了解，而没有全局观念，甚至对自然科学都没有全局观念，更不用说政治、经济、军事等。可是那些没有任何讲道理的头脑和了解这个世界包括科技发展知识的文艺腔来谈环保，就更没谱了。他们就会说：不要讨论争执那些问题了，不要讨价还价了，环保这是人类大事，争什么啊?这是不道德的，我们为人类治理环境做贡献不就完了嘛!有些外国人就这么忽悠中国人，我说你为什么只要求中国这样做而不要求你的同胞去做呢?这种漂亮话颇能得到那些文艺腔的青睐，你要是反对，他们就会破口大骂，说你没有人性。<br />
<br />
　　3.透过几种专业看文科缺陷<br />
<br />
　　按说法律专业应该是非常讲逻辑的，但中国的法律界人士却并非如此。我曾对某些人说过，你们老讲美国式的民主宪政这个道德制高点，我绝不反对，但是，中国要真的实行美式制度了，你们还有饭吃吗?因为他们的辩论能力极差，不会讲道理，要像美国式的庭审辩论的话，只会一败涂地。美国的辩论当然在讲逻辑的同时也会去煽情，但是如果太不讲道理，情也煽不起来。美国由那些法律界的人士来治国是挺恰当的，因为他们一方面有文科那种好处，比如说全局观念，了解这个社会，了解人与人之间的关系，能煽情;另一方面，他们又非常能讲道理。可我就不明白为什么中国的法律人士就不会讲道理，当然，警官类的学校毕业的学生，学刑侦的、学公安的等等，讲道理的能力比法律专业的强，只是警官学校恐怕很多还属于理工科。当然，这跟中国司法腐败是有关系的，很多法律工作者主要不是靠庭辩，而是靠幕后操作，庭外操作，这当然是不需要讲道理的。<br />
<br />
　　中国法律界人士有一种高调，重罚遏制不了犯罪。乍一听挺高尚、挺人道的，但这纯粹是煽情的话，没有任何理性的数据支持，正相反，数据恰恰说明重罚是能够震慑犯罪的，这是明摆着的事儿。比如说对酒驾加重处罚后，酒驾明显减少了。当然，重罚震慑犯罪也会付出社会代价，那另当别论：你可以说社会代价太大，所以我们反对这样做，但你不能说“重罚遏制不了犯罪”。西方也有这种人唱高调的。一个德国教授来做学术报告，他拿一组数据来说明青少年犯罪中受到惩罚的人重犯率比没有受到惩罚的人还高，因此得出结论：重罚不仅不能震慑犯罪，反而引发犯罪。我当场就质问：受到惩罚的那些青少年所犯的罪行跟没受惩罚的所犯的罪行是一样的吗?如果不是，那么，你的数据也许只是说明犯重罪者重犯率更高;如果是，那么你们德国的司法公正何在?你真的可以严格控制所犯罪行这个变量吗?一句话就把他间傻了。这种人为了占据道德制高点，被冲昏了头脑，无法进行理性思考了。<br />
<br />
　　在文科当中，素质原本就很高，又学了比较多的技术和逻辑思维的人集中在哪儿?主要在金融，因为可以赚大钱。但是对于我们这个国家来说，文科优秀人才都集中到金融去了，未必是有利的。学金融专业的人去治国，同样缺乏大局观念，而且搞金融的老是想以投机取巧来发财，往往贬低实业。现在这都成了风气了，这是完全错误的，实业才是更重要的。<br />
<br />
　　历史学，我认为是所有文科一一文哲政经法一一的基础，因为文科没有办法像自然科学那样做实验，用宋代替实验的只有对历史经验的总结。尽管整个历史环境不能复制，今天的东西跟过去都有好多地方不一样，不可能像自然科学可以复制所有其他条件来一次一次做实验，但是依旧能在历史上做借鉴，找到相似的东西，看看当时人怎么处理一些大事情及其兴亡得失等。所以我认为学历史是特别重要的，是所有文科的基础。也许诗词歌赋刨除在外，诗词歌赋是一个感性的东西，你讲的漂亮就可以了。除此，经济学、法学、政治学、社会学甚至金融学，尽管现在很大程度上已经数学公式化了，这样好像更严谨了，但事实证明，数学公式还经常出毛病，还是得以历史为基础。<br />
<br />
　　那么中国现在历史学的情况怎么样呢?我相信我也知道有很多历史学家是在孜孜不倦地进行研究的，从长远看，将来他们的工作一定会产生影响，可能不是一时的，而是千秋万代的影响。不过在此，我们是从时政的角度，是从当前产生社会影响的角度来看，那么浮在面儿上的那些历史学家恰恰是既不好好挖掘史料，又不讲道理的，全是胡话，非常肤浅，无非是利用历史学来哗众取宠。历史学本来是一个非常讲道理的学科，很接近理工科的思路。首先得收集史料、收集资料，这跟理工科的观察自然、观察实验结果有相似之处。其次，要从挖掘的史料当中讲道理，合乎逻辑地推出你的结论来，否则是站不住的。间题是中国现在所谓的历史学者，收集史料他不行，讲道理也不行，他的长处就是文艺腔：词藻花哨和抢占道德制高点。我碰到一个历史学家，他居然说历史学家可以不讲逻辑。像他们这样的人来治国，会比工程师更好吗?<br />
<br />
　　在这样一种情况之下，侈谈说中国过去工程师治国是错误的，应该换成文人治国，我认为是有问题的。不管工程师治国还是文人治国，都有缺陷，若真是要两害取其轻的话，我宁可要有技术没文化的，也不要这种所谓有文化没技术的。可惜这个迷信后者的趋势很强，我发现中国的工程师确实很没有自信。<br />
<br />
　　四、如何实现技术与文化的结合<br />
<br />
　　综上所述可知，1949年以后，理工科的确聚集了中国最优秀的人才，此长彼消，文科在这方面自然就大为逊色了，而且文科知识分子还存在着文艺腔的弊病。因此，尽管理工科知识分子有缺乏大局观等缺陷，但是权衡利弊，他们还是比人文知识分子更有资格担当治国大任。工程师治国强于文人治国，直到今天依旧如此。<br />
<br />
　　当然，这绝非理想状态，最理想的状态，是弥合文化与技术的割裂。从长远来看，政治领导人多为文科出身而又了解科技与逻辑，这样最好。但是，这毕竟不是朝夕之功，需要假以时日。时不我待，从目前的现状出发，如何才能尽快地在最基本的程度上实现文化与技术的结合呢?简单地说，道路不外乎两种：第一，有文化的多学习技术;第二，有技术的多学习文化。两者哪一个见效更快呢?<br />
<br />
　　我倾向于后者，即提倡学理工科的学生多学习点文科的东西，这相比文科生宋掌握自然科学知识要容易得多。其实这也是老生常谈了，学理工科的要改行学文科，包括考研究生及后来的工作，不会感到太大困难;反过来说，学文科的要去弄理工科，比登天都难，简直是没可能。所以要想解决有技术没文化、有文化没技术的问题，我觉得权宜之计，首先还是有技术的入学习文化，然后才是有文化入学习技术。<br />
<br />
　　再以我本人为例。我本科学数学的，硕士想改为经济学。为了考试得现补经济学基础课程，为此，我仅仅是在考前花了两个星期时间就过关了。在报考之前，我曾经征询过我的一个当时正在人大读经济学专业的同学，他也说不用花太多时间就会掌握的，把萨缪尔森的那本教科书吃透就行了。一个在人大经济系这样很优秀的地方学习的学生，毫不怀疑一个理工科的没接触过经济学专业的学生，可以在非常短的时间内，大体上掌握他这个专业的知识，并与他们在研究生考试中竞争，这说明了什么?说明经济系的学生很清楚，其实他们四年就学了这么点东西。要说他们特别有文化，有道理吗?那点文化让有技术的学起来不是很快嘛?或者说如果你考上经济学专业，其实你也可以整天泡妞，根本用不着废寝忘食。<br />
<br />
　　有技术的去学文化比有文化的去学技术要容易很多，所以当务之急应该是提倡有技术的人去学文化。</div>

							
							
							
							
