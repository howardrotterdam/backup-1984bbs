													<h2>韩寒：这样好的人民</h2>
						
						
																											<div id="postmessage_515916" class="t_msgfont">在《韩峰是个好干部》中，我设置了投票功能，到现在一共有21万个独立的IP进行了投票，结果显示，有百分之九十六，也就是20万人觉得韩峰是个好干部，应该留任，有百分之四不到1万人觉得韩峰是个坏干部，应该严惩。<br />
<br />
投票的人中，有人是发自内心的觉得韩峰还算不错，胃口比较小，有人是真情流露的觉得韩峰在官员中已经算是个人修养比较高的，有人是起哄的，有人是反讽的，但是大家都很无奈。我爷爷那会儿，大家都知道有困难，找干部，弄不好干部比你还困难，后来大家都知道，这个国家分好的官员和坏的官员。韩峰的投票结果说明我们正式进入了一个几乎无官不贪，只分好的贪官和坏的贪官的年代。大家认为韩峰明显属于好的贪官。<br />
<br />
<br />
<br />
虽然这几天，韩峰因为在土地交易中受贿被逮捕，但是在报道韩峰的新闻中，我们不应该只注意到韩峰玩手机，玩职权，玩小谭。在《新世纪周刊》的采访中，韩峰的领导这样评价他，“广西的烟草消费量在全国排名靠后，而来宾地区的消费量则更低。韩峰在来宾的政绩之一，就是在2007年使该地区人均消费香烟达到六条以上，超过了全国平均量。”<br />
<br />
<br />
<br />
这样一个美好的地方，原本大家抽烟都比较少，国家设立了一个机构，这个机构里有个官员，让这个原本香烟消费很少的地方人开始大量的抽烟，最后人均消费了六条以上，超过了全国平均量，居然这就是成了好领导的标准。让老百姓多抽烟居然成为了政府部门的政绩。作为一个像样的国家，你不禁烟算了，你怎么能将专门设立一个官方机构旨在让老百姓多抽烟呢，你怎么能用老百姓的健康来换取你这点微不足道的GDP呢。不过我转念一想，也正常，从来都是这么干的。<br />
<br />
<br />
<br />
在两会期间，经常有媒体让我为两会想提案，或者录制一些谈论民生的节目，甚至去北京和两会做近距离的接触，我都推辞了，我一个草民，我去做什么呢，第一，我并不缺笔记本电脑，第二，都说了我不演戏的。<br />
<br />
<br />
<br />
但是，在两会结束之际，我想说，其实，我们的政府真的是很幸福的。大部分的老百姓永远认为，上头的政策是对的，只是给下头执行歪了。几十年之后，他们还是认为，上头的政策是对的，只是下头怎么老是执行歪。他们从来没有怀疑过第一句话，他们对“上头”还是抱着最原始的信任。屁民们遇见了委屈，能做的最后一件事情就是上京告御状，虽然信访办更多的职责是负责登记新增的监控对象和运输他们。在遇到村长欺负的时候，他们会想到镇长，镇长不管的时候，他们会想到县长，县长不管的时候，他们会想到市长，但是他们永远找不到市长，他们于是幻想着中央的部长，甚至更高的领导，他们觉得只是被下面这些领导们阻断了他冤情的传达，他们从来不曾想到，他想见的人说不定早觉得他烦死了，背地里可能就耍下一句没有大局观。他们从来只是在讨要自己的一点点权益，从来不去要求自己有什么权利，他们从来只是觉得是地方官员的问题，而不是其他什么问题，他们只要有一个坐着奥迪的领导在过年的时候慰问一下就觉得非常温暖，当然，那也需要他不光穷，而且穷的正确，穷的典型。他们觉得韩峰这样就已经不错了，他们对干部的指望并不是为老百姓服务，而是不要找老百姓的麻烦就行了，你住你的豪宅，坐你的好车，玩你的小谭，咱已经都不管了，只要你不要踩我的小强，拆我的民房，弄我的小芳，你就是老百姓心目中的好官。如果网民讨厌，删帖就行，如果作家讨厌，和谐就行，如果记者讨厌，只要一句话――最近不要报道负面新闻就行。<br />
<br />
<br />
<br />
所以说，这样的政府是幸福的，你们拥有者这样淳朴温良容易满足的老百姓，他们虽然有很多不满，但他们有着最底线的信任。有的时候他们聚众闹个事，最多就是因为本来政府答应他们只要把自己的蛋糕给政府，政府会给他们一个包子，结果只给了一个馒头。你只需要从牙缝里剔出一点肉给他们当馅，他们就满意的回家了。面对这样的人民，我真的愿望政府可以忘记GDP的荣耀，让出一个点，在开会的时候少说一点排比句，多分一杯羹给大家，让他们少一点生活压力，庇护他们，罩着他们，让他们有点尊严，而不是通过新华社通稿被获得。<strong>你要是把这样好的人民给饿死了病死了穷死了逼死了毒死了吃死了气死了冤死了喝水喝死了，你去哪里找比他们更老实的人民呢。</strong></div>

							
							
							
							
