													<h2>中国只有坚持共产党的领导 这是历史的选择</h2>
						
						
																											<div id="postmessage_740955" class="t_msgfont">看这个标题很多愤青要拍砖了，我本人刚来，也知道这里要民主的人多，但请听我给你讲讲国内论坛讲不了的东西，你就明白，我为什么说一定要坚持共产党的领导中国。<br />
<br />
<br />
<font face="微软雅黑 ">首先我们必须要把一个事实放出来，就是当今的中国，是秉承国父孙中山的以党治国理念，有苏联的影子，也有中国迫切需要民族独立的历史机遇形成。今天之所以成为专政，我认为是历史选择了共产党，也是中国历史进程中必须产生这样一段过程。并且是漫长的，稳定的。我们经常提到国父孙中山先生的三民主义，也讲到他提出的军政p训政p宪政，尤其是国民政府在台湾似乎完成了最后一个阶段，但是我们应该注意这其中的逻辑，就是孙本人提出的三个阶段，是没有明确提到时间表的，就是说A到B到C，可能是10年，也可能是100年，甚至是400年。虽然台湾先完成了，但不代表大陆也可以这样快速，情况差别很大很大。现在大陆应该就处在军政和训政之间。如果假设国民党没有丢掉大陆，大陆依然可能处在这个阶段.。<br />
<br />
<br />
中国历来多的是鲁迅笔下的阿Q，奴才以及吃人血馒头的人，少的就是具备汉族高贵基因品质的。放眼近代史，最早具备反抗封建奴役与民族独立主义的汉人基本是海外留学生，数量就稀少，孙中山，蒋介石，汪精卫，周恩来，朱德，刘少奇，张国焘，包括后辈邓小平等等基本是留学生，他们醒悟的比较快，从发表文章唤醒国人没多久，就进入实质操作阶段，孙中山是最成功的，也是最有能力的，中国后来的一系列变革都从他开始。当然包括黄兴等等革命党人，实干能力都首屈一指！他们联合国内例如天地会，哥老会等进行反清活动反满运动，其实就是民族独立，可以说是汉人中绝对智慧的代表，具备高贵的品质，大家想想，那个时候中国汉人处在什么精神状态，大多数就是现在说的奴隶，反正吃饱饭就OK了，有烟草抽那更爽了。而这些革命党人可以说是完全脱离汉人奴性的领袖级人物，这其中就包括后来的共产党人。<br />
<br />
早期共产党人所具备的汉人高贵基因可以说是完美的体现了汉人能够在整个世界民族屹立。 “清党”以后，共产党人的命运可以说是灭顶之灾，整个广州地区其实包括全国大部分人能待的地区几乎已经没有生存下去的地盘，而且他们人数少力量单薄，逃到陕甘宁的时候，只有2000多人。而国民革命军那个时候已经接近百万，并且全国各地的军阀基本上对剿共这个事情都表示同意，就算是出工不出力，但对共产党人来讲，也不是好事。在逃亡陕甘宁的这个过程中，其实可以有很多选择，可以选择和国民政府合作（就好比现在有些民运），也可以卸甲归田去苏联呆着（就好比六四或现在的那些民运逃到美加或东南亚国家），甚至可以像现在有些人那样不满而“自焚”抗议。但是我们所看到的，是在后有追兵前途糜烂的逃亡，已经不惜一切代价的坚持到了最后。他们逃到了人都不愿待的区域，并且待了好几年。这种执着，信念，追求并不是随便哪个汉人能够做到的，你叫今天那些个靠写民主文章混饭吃的朋友去延安窑洞待一个月试试，我包你发疯！笔者本人去过延安旁边的樊川办事，待了4天我就跑了，你可以去体验一下，去亲自试试，也别再讲什么翻雪山了。而周恩来，毛泽东，朱德等这些共产党人完全可以过那个时代中产阶级甚至可以说是比较富裕的生活，待在那个地方好几年。对信仰的追求，对信念的执着，对尊严的渴望！你不得不佩服。你像现在有些人也讲追求民主是他们的信仰追求，在QQ群里，论坛里，SKYPE里大谈自己崇高的民主追求，高举民主理论挥舞，神采奕奕！结果呢，当地一个小国安就一杯茶功夫，保证书也写好了，手印也按了，一把屎一把鼻涕表示要痛改前非，不要告诉单位，不要影响家庭孩子等等。<br />
<br />
共产党依靠中日战争的历史机遇，苏联的援助获取了最后政权，这中间，尤其体现了他们的智慧和经营能力。在和国民党籍的汉人精英最后较量中，很好的利用优势项目，取得最后胜利，各位不妨想想，从逃到陕甘宁的2000多人，对抗上百万政府军，到最后夺取政权，共产党人难道不是汉人的高贵品质代表？今天有些网友看人做事很容易，说什么共产党是靠忽悠农民发家的。我觉的，你这样眼高手低很无知。你去试试忽悠别人，这不是忽悠买个残疾车，而是让他去打仗，和几百万政府军对抗，和飞机炸弹拼命，你去试试，看看有人听你吗？别忘记，当年很多共军新兵和现在你手里的武器是一样的，一把砍刀！直接上战场！城市里的那些地下党，你看看他们，相当多是中产阶级，你去试试，你去找一个像余则成的为你卖命，看看有人理睬不？所以我就说，不要看人做事很轻松，一旦你去做这个事，你会发现，根本做不了。412后，共产党人可没想现今的右派天天喊平反，也更没像很多右派那样躲在角落里写文章骂国民党骂社会，无论力量对比多么悬殊，他们不怕。这种大无畏的气魄，这种汉人最最珍贵的品质，有多少人拥有！从来不求做稳了的奴隶。这种品质你有吗？大家说，共产党统治汉人合理吗？当然合理！<br />
<br />
你没有他们的品质，其实很多汉人人都不具备。就是现在有强权叫你扎辫子，你也会扎，你信不信？其实现在大多数的中国人都在精神上扎着，起码能做稳了的奴隶，我本人也是汉族的劣根代表之一，你让我去“陕甘宁”我肯定吃不起那个苦，更不可能扛枪打游击闹革命， 我顶多躲在墙外写写层次很低的文章，或者转转帖，我根本不可能会为了信仰去牺牲自己的性命。所以共产党人作为比较先进的一种汉族品种来统治汉人，我认为是合理的。即使他们拿得多也是合理的，因为就算他们不拿，也会有其他高贵的民族来奴役我们。你看，很多右派不是喊美国大兵来救嘛。他们自己属于汉人品种中比较劣质的，所以一定要找好主子。共产党不好了，就找美国的，美国不好了，找台湾的，反正就是一定要找好主子才能安生立命。左派也一样，现在走资派主子不好，就梦呓老主子。<br />
