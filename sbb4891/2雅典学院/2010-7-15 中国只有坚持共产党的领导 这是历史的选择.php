<br />
　　目前再来公然反对民主政治，似乎是不可能了。目前再来企图根本否认民主政治对于抗战的重要，似乎是不可能了。于是反民主政治论者就来一套新的法宝。他们说，民主政治是好的，但是我们的国民不好，不能实行。他们说，我国人民素来缺少教育和自治的训练，如果贸然实行，就要产生不好的结果。所以现在实现民主政治是不可能的。他们以为中国实现民主政治，不是今天的事，而是若干年以后的事，他们希望中国人民知识与教育程度提高到欧美资产阶级民主国家那样，再来实现民主政治。<br />
<br />
　　他们好象忘记了中国今天是处在艰苦的抗战中，忘记了中国今天来实现民主政治，不仅是历史发展普通的一般的要求，而且是抗战特殊的迫不容缓的要求。至于民众教育程度和自治能力的培养，这自然是一个非常重要的问题。但要知道，民众的参加抗战动员，民众自身的民主生活，是他们受到训练和教育的最好、最迅速的方式。正是在民主制度之下更容易教育和训练民众。在这种方式下，群众可以充分发挥其积极性和创造性，把他们自身的教育和抗战事业，同时推向前进。中国正开始向着民主政治的道路前进，这是抗战的需要。在这方面，我们的成绩虽还不大，但在全国上下共同努力之下必能更加进步。<br />
<br />
　　今后的努力方针在于：第一，加强民意机关的发展，把现在的国民参政会和各省正筹备中的参议会，逐渐变为真正代表人民的机关；第二，切实执行抗战建国纲领所规定的“于抗战期间不违反三民主义最高原则及法令范围内，对于言论出版集会结社予以合法之充分保障”。这是提高民众教育程度和自治能力的有效方法，也是实现民主政治，加强民众动员争取抗战建国最后胜利的重要任务。<br />
<br />
――摘自《新华日报》1939年2月25日社论《民主政治问题》</div>

							
							
							
							
