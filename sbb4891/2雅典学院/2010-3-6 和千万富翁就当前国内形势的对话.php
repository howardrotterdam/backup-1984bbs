																											<div id="postmessage_505559" class="t_msgfont">这个千万富翁就是我的前任老板。正因为没有了利害冲突，我们可以敞开心扉地讲话。<br />
<br />
先说说我老板的背景吧，老板一家有曾在高校做学问的，有从政的，也有经商的。有做二线地产商的，也有在流通领域里做实业的，做的都不错。老板自己早已做成当地该行业民营企业的老大。资产早已过千万。<br />
<br />
我：为什么房价这么高？<br />
老板：高是市场决定的。上海房价贵不贵，2W一平米的房子却买不着。房价无论多么高，总有人在买。<br />
<br />
我：温家宝从上台就开始调控房价，房价一直上涨，房价会跌吗？<br />
老板：跌？只会上涨！国家储蓄率多年奇高不下，只有房产和汽车这些大宗消费品可以打开缺口。让储蓄压力有地方释放。<br />
<br />
我：那是因为国企掏钱买政府地皮，国家右手掏钱又回到左手。<br />
老板：你说的地王毕竟是少数。多数开发商仍然拿的到能够承受的地皮价格。汤臣一品是贵，全上海不就一处嘛。<br />
<br />
我：为什么大学生找不到工作？<br />
老板：大学生不是找不到工作，是不愿意找。找不到工作的都是有着这样那样的要求而用人单位不能满足的，或者是自己压根就不愿意工作。<br />
<br />
我：您对山东的大学生为了工作，只能去掏粪怎么看？<br />
老板：还是那句话，谁也没说那几个大学生为什么去掏粪，总有他自己的道理。你也不知道他们的心里是怎么想的。<br />
<br />
我：您对美国最近的几宗对国内出口产品制裁怎么看？<br />
老板：我同意美国那位华裔商务部长的讲话，中国每年向美国出口那么多产品，制裁的份额只有那么一点。美国的做法并不过分。<br />
<br />
我：可是对于中国出口企业来说，美国一旦制裁，国内的一个行业就要死掉。<br />
老板：那可不是。你知道出口轮胎被征收反倾销税后，国内轮胎企业今年多么红火吗？订单都做不过来。因为内需非常大。<br />
<br />
