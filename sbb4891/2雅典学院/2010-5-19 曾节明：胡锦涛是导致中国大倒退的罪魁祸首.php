													<h2>曾节明：胡锦涛是导致中国大倒退的罪魁祸首</h2>
						
						
																											<div id="postmessage_624337" class="t_msgfont">文章摘要: 胡锦涛根本不是什么“开明派”，江泽民之后，中国社会发生的大幅倒退、中国人权状况的全面恶化，就是胡锦涛主导所为，就是胡锦涛的本意！<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;作者 : 曾节明,<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;l表rg：5/17/2010<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;胡锦涛上台近八年来，中国社会全面倒退十分明显：政治上重新集权，加强以党凌政、党控司法、重新强化居委会等基层党组织，新闻出版上扼杀江时期民营出版业萌芽、以“六四”后未有的力度整肃媒体和出版社、厉行媒体一律、大肆禁书、禁播、对互联网的封锁变本加厉，一再大幅升级...大大收紧言论钳制尺度，公开反对自由民主等“普世价值”、炮制《六个为什么》、恢复大规模直接意识形态洗脑，重判刘晓波、严酷镇压自由派知识分子，摧毁国内非政府NGO组织、大力封杀江后期隐然成型的市民社会，重树毛泽东旗帜、大力纵容毛左派、开“改革开放”三十年来镇压维权上访之先河，在经济上，扭转江泽民时期市场化方向，厉行 “国进民退”、重新扶持国企扩张、强化政府对土地垄断、制造房地产泡沫、纵容全面强迫拆迁、强迫征地，以卖地经济保政权....目睹如此恶劣的事实，近年来已在中国人权问题上态度温吞的美国，都作出了“中国人权状况全面恶化”的评价1。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;倒退的趋势是如此的明显和恶劣，以致于许多看好胡锦涛的人，也不得不承认倒退的事实，但他们却普遍把胡锦涛上台以来发生的所有倒退，说成是江泽民“垂帘听政”的结果。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;周亚辉先生就是持这种观点的典型：大约在2009年下半年，自称是澳大利亚华人的周亚辉先生出现博讯上，时常一日数贴地为胡锦涛辩护，周先生想当然地认定胡锦涛是改革派，并断定所有的倒退都是江泽民造成的，我感慨于周亚辉这样“海外华人”（如果他真是海外华人的话），对胡锦涛误解之深、对中共现政权常识之匮乏，因此愿在此关键处再做一番分析（尽管我以往多次剖析过胡锦涛其人本质真相），以供读者参考。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;那种认为胡锦涛上台后的倒退是江泽民造成的观点，至少带有以下无法自圆的大破绽：<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;首先，江泽民时期的大部分时间（1992年――2002年），比胡锦涛时期开明和宽松是客观事实；江泽民真要倒退，在其任上何不轻松实行，反而在退职后费力地间接实施？<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;有人认为江泽民此举是要搞臭胡锦涛，再伺机“复位”，这种解释更站不住脚，一则因为中共政权与民意无关，倒退的臭名损害不了独裁者的权力，二则因为重趋集权的倒退只有利于胡锦涛独裁，而不利于江泽民卷土重来。那么，江泽民去搞这种费事不得好的极权倒退为哪般（如果江泽民真能“垂帘听政”搞倒退的话）？<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;第二，如果真如周亚辉先生所说的：胡锦涛上台后没有实权，一直是江泽民在幕后掌权，那么江泽民的铁杆亲信，上海护巢留守陈良宇怎么会倒台？江泽民的另一心腹黄菊怎么会失势？胡锦涛作为总书记、国家主席和军委主席，既然有权力抓捕陈良宇，八年来就没有权力减轻一点暴政？哪怕是一丝一毫的减轻？<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;第三，江泽民“退而不休”是事实，但江泽民远没有邓小平的权威也是公认的事实。稍有头脑的人可以看到这样的对比：当年胡耀邦、赵紫阳在邓小平、陈云领衔的 “中顾委”掣肘下，仍然可以保护“自由化”人士和团体、大力推进新闻出版自由、筹备党政分开、播出《河觞》...如果胡锦涛真是开明派的话，为什么他在能量远不及邓小平、陈云的江泽民的“掣肘”下，反而什么开明的举措都没有，有的只是极权倒退呢？<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;以上破绽是如此明显，但周亚辉先生却不愿面对，他的理由是：九常委中李长春、贾庆林、吴邦国、周永康、习近平都是江泽民的人马，胡锦涛在九常委中只有一票，因此，江泽民得以通过常委中的多数票制定了倒退政策；周亚辉先生还咬定：刘晓波是周永康抓判的，与胡锦涛无关。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;这就反映出以周亚辉先生为典型的群体，对中共现政权常识性的无知。现今中共政权，意识形态早已破产，一把手的权威基本上是由职务衍生的职权之威，而非个人魅力和个人人格之威，邓小平之后的中共领导人，离开了职务很难有“权威”可言。在邓小平强人时代终结后，统治集团内已很少有人身依附关系的个人效忠，李长春、贾庆林、吴邦国、周永康等人过去与其说效忠江泽民，莫如说效忠他们自己的既得利益，一旦江泽民因失去了职权而不能再充分起到他们保护伞的作用，这些人为保既得利益，不可能不转投新皇帝胡锦涛所好，否则，贪腐劣迹斑斑的他们，就有步陈良宇后尘的危险。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;这，在李长春、吴邦国身上体现得尤其明显，李长春八十年代时是地方干部中的改革派先锋，在江泽民时期也不是顽固派，现在却整一个左棍腔调；而老滑头吴邦国，在江时期主政上海时支持“思想解放”，现在却公开反对“普世价值”…贾庆林的情况也是如此。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;由于江泽民远没有邓小平的淫威，没有能力在失去职位的情况下，通过常委中的人马主导中国倒退，李长春等人的转变不能不说是“上（胡锦涛）有所好，下必甚焉”。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;周亚辉先生的另一大误解是认为中共总书记只有常委会一票的权力。众所周知，中共党内施行的也是假民主――或曰“民主集中制”，在下级服从上级的铁律下，民主是假，集中（独裁）是真，因此投票不过是过场，政治局常委会同样如此，投票之前，已经由一把手定调，常委会中，总书记举足轻重、一锤定音，而并非由多数常委说了算，例如：江泽民发起镇压法轮功，就没得到多数常委支持；既然没有多数支持，为何仍然推出了镇压法轮功的政策？这能说明中共总书记只有常委会一票的权力？<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;事实上，由于直接掌控宣传、军委、政法委大权，中共总书记拥有无上自主行事权，象收紧舆论控制、加强封网、军管“维稳”、镇压维权、扫荡NGO、批判“普世价值”、重树共产意识形态等极权倒退措施的施行，根本无须经过政治局常委会，集总书记、军委主席、国家主席三权于一身的胡锦涛完全可以在日常工作中自行其是。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;认为刘晓波是周永康抓判的，与胡锦涛无关，则属于此种谬误中最为荒谬的观点。首先，依照中共政权的运作规矩，象刘晓波案这样具有重大国际影响的政治大案，属总书记和国家主席胡锦涛重点过问的大事，根本不是政法委书记周永康能够决定得了的；其次，按照中共组织原则，政法委书记在总书记领导下工作，因此，若没有一把手胡锦涛的批示，并没有多大能量和权威的周永康既不敢、也犯不着擅自做主把刘晓波抓起来重判十一年。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;周亚辉先生对以上情况统统不察，为了证明胡锦涛是“改革派”，他还搬出胡耀邦提拔胡锦涛的往事。的确，胡耀邦于1985年，将胡锦涛由甘肃建委提拔至团中央是事实，但胡耀邦提拔胡锦涛，不能说明胡锦涛就是改革派；比如，胡耀邦还力排众议任用薄一波，薄一波是改革派吗？<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;事实上，胡锦涛是党内顽固派宋平、邓力群培养并大力举荐的人，他从来不是胡耀邦的追随者；宋平是中共顽固派大佬陈云的大徒弟，性格软弱的胡耀邦不敢不买陈云的帐。胡锦涛内心对胡耀邦可能怀有一些感激之情，但在治国理念上两人根本格格不入，这从两人对西藏的政策上可以充分地看出来。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;胡锦涛从来就不是什么“改革派”，还可以从他的经历、性格和他的言行举止中看出来。胡锦涛思想塑形的大学时代，正是上世纪五十年代毛共的黄金岁月，他以其又红又专的楷模表现，被任用为大学辅导员，可见其毛共思想深入骨髓，而且，在整个毛时代，胡锦涛一帆风顺；这与戈尔巴乔夫青年时期深受赫鲁晓夫否定斯大林之启蒙，截然相反。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;胡锦涛的人文素养也根本撑不起“改革派”的资质，与戈尔巴乔夫、赵紫阳博览西方名著相反，胡锦涛平时不学无术、熟读仅有卓娅与舒拉、斯大林和毛泽东的篇章。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;从胡锦涛对刘军宁和老上级朱泽厚的态度可以看出，此人貌似忠厚，实则嫉妒心极强、心胸狭窄、残酷无情、手段阴狠；他和与人为善的胡耀邦根本是两种人，这样的人是不可能有推行政治改革的道德和胸襟的。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;除了上台之初，立足未稳之际唱过两句“以人为本”、“依宪治国”的花腔外，胡锦涛上台后没有任何开明的作为，反而随着权力的巩固，倒退言论不断加重、倒退步伐不断地加大：上台伊始就发起远比江泽民“三讲”更左、更荒谬的“保先”运动、2004年九月接掌军委主席当天，即发表政治学朝鲜、古巴的讲话（即“九一九”讲话）2；2005年五月又发出杀气腾腾的五一五讲话，重提“反自由化”、斥巨资扶持高校马列学院…2008 下半年开始，更公开发起批判“普世价值”运动、并炮制《六个为什么》，再次施行大规模的意识形态洗脑工程……<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;分析这里，中国八年来大倒退的原因也就出来了，答案只有一个，且如秃子头上的虱子，那就是，胡锦涛根本不是什么“开明派”，江泽民之后，中国社会发生的大幅倒退、中国人权状况的全面恶化，就是胡锦涛主导所为，就是胡锦涛的本意！<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;最后需要说明的是：我做此番评析，不是要为江泽民辩护，而是希望提请同胞们克复中国人“麻将思维”的弱点，具体问题具体分析，不要搞笼统化、相对（无所谓）化，江泽民的罪归江泽民，而胡锦涛必须承担他上台八年来犯下的累累罪行。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;我想特别提醒某些群体：当前当权的独裁者是胡锦涛而非别人，胡锦涛是一个标准的顽固派中共党棍，是比江泽民远为凶恶的中国民主化当前大敌，是中共现政权的驾驶员和中国极权势力的总代表，如果不旗帜鲜明地反对胡锦涛倒行逆施，反而把今天的迫害当成昨天的迫害，把2010 当作1999，继续对更凶恶的新独裁者浮想联翩的话，反迫害反专制的抗争就会无的放矢、就会陷于空泛化、泡沫化，甚至无意中沦为衬扶新独裁者的帮腔造势的工具。<br />
