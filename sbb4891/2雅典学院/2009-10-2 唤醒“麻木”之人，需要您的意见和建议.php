<br />
　　“没有什么用。”&nbsp;&nbsp;<br />
<br />
　　“那么，你钞他是什么意思呢？”&nbsp;&nbsp;<br />
<br />
　　“没有什么意思。”&nbsp;&nbsp;<br />
<br />
　　“我想，你可以做点文章……”&nbsp;&nbsp;<br />
<br />
