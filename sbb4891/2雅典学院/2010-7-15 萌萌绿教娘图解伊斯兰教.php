这个有断章取义的嫌疑。<br />
原文是：<br />
&lt;&lt;&lt;&lt;&lt;&lt;<br />
一九○　你们当为主道而抵抗进攻你们的人　你们不要过分　因为真主必定不喜爱过分者　<br />
一九一　你们在那里发现他们　就在那里杀戮他们　并将他们逐出境外　犹如他们从前驱逐<br />
　　　　你们一样　迫害是比杀戮更残酷的　你们不要在禁寺附近和他们战斗　直到他们在<br />
　　　　那里进攻你们　如果他们进攻你们　你们就应当杀戮他们　不信道者的报酬是这样<br />
　　　　的　<br />
一九二　如果他们停战　那末　真主确是至赦的　确是至慈的　<br />
一九三　你们当反抗他们　直到迫害消除　而宗教专为真主　如果他们停战　那末　除不义<br />
　　　　者外　你们绝不要侵犯任何人　<br />
&gt;&gt;&gt;&gt;&gt;&gt;<br />
<br />
（9－123）你们要讨伐邻近你们的不信道者，使他们感觉到你们的严厉， <br />
这个就是本义了。<br />
原文是：<br />
&lt;&lt;&lt;&lt;&lt;&lt;<br />
一二○.麦地那人和他们四周的游牧的阿拉伯人，不该逗留在后方，而不随使者出征；不该只顾自己的安逸，而不与使者共患难。因为凡他们为真主而遭遇的饥渴和劳顿，他们触怒不信道者的每一步伐，或每次对敌人有所获，每有一件就必为他们记一功，真主一定不使行善者徒劳无酬。<br />
一二一.他们所花的旅费，无论多寡，以及他们所经历的路程，都要为他们记录下来，以便真主对他们的行为给予最优厚的报酬。<br />
一二二.信士们不宜全体出征，他们为何不这样做呢？每族中有一部分人出征，以便留守者专攻教义，而在同族者还乡的时候，加以警告，以便他们警惕。<br />
