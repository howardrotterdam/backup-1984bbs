													<h2>唯薄熙来敢政变？</h2>
						
						
																											<div id="postmessage_799304" class="t_msgfont">郭松民<br />
薄熙来在重庆接见大学生，引用毛主席语录“看一个青年是不是革命的，拿什么做标准呢？拿什么去辨别他呢？只有一个标准，这就是看他愿意不愿意、并且实行不实行和广大的工农群众结合在一块。愿意并且实行和工农结合的，是革命的，否则就是不革命的”与同学们相勉励。有同学说：我们这个团队还意穿上了红色的 T恤衫，因为我们是红色的接班人。薄熙来则称赞说：大家是共青团嘛，就是要坚持我们的理想信念！我们就是要“唱红”，就是要举红旗。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;举红旗，是一个很勇敢的说法。因为现在官员只愿意说举旗帜。红旗是色彩鲜明的，没有任何含混之处，旗帜就暧昧多了，我们不知道旗帜是什么颜色的，甚至不知道它是星条旗、米字旗还是膏药旗。 (博讯 boxun.com)<br />
<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 这样的语言，这样的气氛，再配上一张薄熙来和上百位身着“红装”，青春逼人的大学生的合影，我感觉重庆有一点当年陕甘宁边区甚至延安的意思了，这种感觉也是久违了的。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 2007年，当薄熙来从商务部长任上，“左迁”到重庆担任市委书记时，海外媒体普遍认为他遭到了“贬谪”，但现在看来，这次“贬谪”反而成就了他，因为对一个天生的政治家而言，市委书记这种主持全面工作的岗位，显然更有利于他实现自己的政治抱负，而商务部则专业性太强，实际上更适合职业官僚。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 薄熙来早在1990年代初担任大连市长的时候，就因为成功地把大连建设成了花园城市而成为“明星官员”，但直到他在重庆唱红打黑，干得风生水起之前，我也并未过多地关注过他。因为从早期的步鑫生、马胜利，到后来的仇和等，这些“明星官员”们都有一个共同的特点，那就是他们的“铁腕”主要是用来帮助资本，对付大众的，他们很善于抓GDP，并由此获得了政绩、声望，但留下的往往是更大的贫富差距，更加腐败的官场。薄熙来似乎比他们出色一点，坊间早就传说，在他的治下“官不聊生”，但也仅此而已，似乎和其他“明星官员”也没有什么本质的区别。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 但当薄熙来到了重庆之后，他早年的红色记忆似乎在一夜之间苏醒了。和别的地方把招商引资看得高于一切，重于一切不同，重庆的特色在“唱红打黑”，“嘉陵江边红一角”，薄熙来终于显露出了他作为“红二代”的本色。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 去年年底，我在接受一家电台采访时曾经说：从实际政治效果来看，重庆的唱红，并不仅仅是怀旧，而是为了唤醒共产党人早期的红色理想，这个理想就是要建立一个“人人平等，人人有工作，人人有饭吃，没有人剥削人，人压迫人的新社会”。薄熙来是在通过唱红，来争夺文化领导权，为打黑来营造一个理直气壮的舆论氛围。而重庆的打黑，也不仅仅是维护社会治安这么简单，因为其规模之大，涉及的官员、“民营企业家”之多，使其明显地带有调整利益格局的性质，甚至可以说，重庆在进行一场“没有被称为革命的革命”，而重庆老百姓的欢欣鼓舞，也让人清晰地感到，他们有第二次被解放的感觉。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 我最后说：中国要继续前进，就必须调整目前的利益格局，要调整利益格局，就必须有人要让出利益，而在经历了以原社会主义体制为对象的三十年改革之后，普通的工农大众已经一无所有，甚至成为“负翁”，没有任何利益可以继续出让，所以调整利益格局就必须对现有的既得利益阶层，即在改革开放中富起来的那部分人动手。中国过了这一关，从此就进入和谐社会，今后可以从容应对各类国际风险，过不了这一关，就相当于在长江航行总出不了三峡，以后就很难说了。从这个意义上说，薄熙来在重庆的实践，是在为中国寻找出路，是在为中国“趟雷”，我不理解中央为什么对重庆的实践始终不表态？<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 当然，如果真的以“革命”为标准的话，那么薄熙来在重庆做的还是非常有限的，比如他根本还没有涉及到生产关系层面。但我想，薄熙来已经在他能做的范围内，做到了最好。要知道，当年在陕甘宁边区，也停止了土地改革的政策，而改为“二五减租”，这是宏观的形势和革命的阶段性所决定的。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 中国的“自由派-新右派”则一直是带着狐疑，挑剔的眼光来看待薄熙来在重庆的所作所为的。待到“李庄案”发生之后，他们感到总算抓到了什么把柄，立刻大哗，什么“黑打”呀，什么违反“程序正义”呀，似乎他们多么热爱法治，多么热爱正义，其实根本不是那么回事。“自由派-新右派”从来都是“政治挂帅”，“以阶级斗争为纲”的（单从这个角度来看，他们比新老左派更像是毛主席的好学生），比如小布什绕开联合国，用一个捏造的理由侵略伊拉克，完全是黑社会的做派，哪有什么程序正义可言？但当美军攻陷巴格达时，中国的自由派还不是一片欢呼，甚至盼望美军坦克有一天能够出现在天安门广场？再比如，中国的国企改制出现了多少黑社会的手段？但由于符合“私有化”的政治正确，自由派仍然在为之百般辩护。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 中国的“自由派-新右派”反对薄熙来的真正理由其实只有一个（而这个理由又是永远说不出口的），那就是薄熙来在重庆的实践，展示了这样一种可能性：只要共产党愿意调整路线，回归自己早期的红色理想（这种理想主要是被毛泽东所代表的），就仍然可以获得大多数人民的支持，重建自己的执政合法性，而不必进行以西方模式为蓝本的所谓政治体制改革―― 这就等于要了中国的“自由派-新右派”们的命！因为这意味着他们三十年来所进行的“启蒙 ”都白费了，意味着他们可能永远看不到自己的政治理想实现的那一天了。所以他们宁愿看到共产党烂下去，那怕中国也一起烂下去也在所不惜，而不愿意看到共产党有任何恢复健康的可能。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 其实，如果从世界范围内来看，薄熙来的做法其实是非常正常的，任何一个国家民族，任何一个政治运动，在遇到困难时，都会下意识地到自己的先辈和文明源头那里去寻找灵感，因为那里保存着最原始、最纯真、还没有被后人出于自己狭碍的短期利益所污染的理想。西方的政治领袖和思想家动不动就会到古希腊、古罗马那里去翻箱倒柜，美国人更是动不动就把“国父们”的教诲搬出来；伊斯兰世界则喜欢到《古兰经》那里去寻找灵感，伊朗伊斯兰革命的胜利，伊斯兰世界“原教旨主义”的勃兴，也不是偶然的，而是人类这一思维和行为习惯的逻辑结果。中国的“自由派-新右派”对毛泽东不断进行妖魔化的目的，也正是为了杜绝社会主义复兴的任何可能性！<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 所以，薄熙来作为当代中共领袖群体中的一员，当他要解决自己面临的各种问题时，把目光转向中共早期的红色理想，并依据这种红色理想来矫正现实中存在的各种弊端，实在是再正常不过了。改革开放以后，官员和精英们只喜欢谈“创新”，而不愿意谈“坚持”，他们实际上不懂得（或假装不懂）这样一点：创新只能是方法上的，理想则只能坚持，不能创新，对理想进行创新，说得温和一点叫 “变质”，说得严厉一点，则叫“背叛”，如果用宗教的语言说，就是叫“异教徒”了――这里要强调说明的一点是，普通老百姓可以成为“异教徒”，他们拥有自己的信仰自由，这是他们不可侵犯的权利！任何人不得因为他们拥有不同的信仰而迫害他们，但共产党员没有信仰自由，如果你不信仰共产主义了，你可以退党，退党之后你就是普通公民了，就重新拥有了信仰自由。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 许多人喜欢揣测薄熙来的动机，而我历来反对这样做。 “自由派-新右派”揣测他的动机，是为了对他做诛心之论，比如说他有野心，想冲刺十八大等等。左派揣测他的动机，则暗含着这样一种危险，即他们可能用支持某个具体的人，来取代对路线的支持，而我一直认为，路线才是最重要的，政治领袖也要经过路线的检验――如果他坚持了正确路线，则支持，如果他离开了正确路线，就反对，至于他内心深处的动机，并不重要，也不必揣测。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 但对薄熙来本人来说，动机却是至关重要的，因为这关系到他自己能够走多远？假如薄熙来真的像中国的 “自由派-新右派”揣测的那样，目的仅仅是为了在十八大常委名单中获得一个位置，那格局就太小了。在我看来，历史实际上为薄熙来提供了前所未有的机会，面对这样的机会，薄熙来如何选择，将决定他未来的历史地位。如果他作出了正确选择的话，那么若干年后再回过头来看，“十八大常委”的位置其实并不重要，有了当然不错，没有也无妨。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 那么，这是一个什么样的机会呢？这个机会就是：假如我们用大历史的眼光来看，我们就会发现，我们正处于人类历史上一个空前黑暗的时代！说它空前黑暗，不仅是因为自马克思主义诞生以来，人类追求建立一个没有剥削，没有压迫的理想社会的努力遭到了灾难性的全面失败，还因为这种理想本身也被玷污、被嘲笑、甚至被踩到了脚下，这是人类历史上所从来没有过的。但正是因为已经到了最黑暗的时刻，也就意味着黑暗即将过去――这正如子夜是最黑暗的时候，但子夜恰恰预告了黎明的到来。由于按照新自由主义的蓝图建立的世界秩序，必然反对大多数人的利益，所以它也必然会被大多数人所反对，因此，世界现在也正处于社会主义运动全面复兴的前夜。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 面对这样的历史机遇，薄熙来有两种选择：第一种，既然历史为自己提供如此之好的机会，那么就充分发挥自己的智慧、经验和影响力，勇敢地举起红旗，站出来领导社会主义复兴运动，甚至成为国际共产主义运动从低潮走向高潮的关键性领袖人物（由于中国有光荣的革命传统和中国作为世界工厂的现实，更由于中国在资本主义世界体系中的边缘地位所决定的国内矛盾特别尖锐且无法向外转嫁的现实，所以中国必将在新的革命高潮中成为世界革命的中心），最终实现自己的政治抱负并奠定自己的历史地位；第二种，只考虑自己的权力和荣华富贵，目前在重庆所做的一切，只是为了在十八大上被“招安”，最终成为“接轨派”中的一员，那这就是比较令人遗憾的。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 无论薄熙来做何选择，历史都将会按照自己的规律前进。但如果薄熙来作出了正确的选择，则会成为推动历史前进的巨人，如果薄熙来作出了不正确的选择，则会成为历史站台上落落寡欢的误车者，满怀惆怅地看着历史列车呼啸远去（我相信他还不会成为阻碍历史前进的人）。薄熙来将如何选择？且让我们拭目以待吧！ _(博讯自由发稿区发稿) (博讯 boxun.com)<br />
