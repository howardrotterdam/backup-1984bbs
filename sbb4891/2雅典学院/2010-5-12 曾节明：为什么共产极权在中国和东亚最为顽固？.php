													<h2>曾节明：为什么共产极权在中国和东亚最为顽固？</h2>
						
						
																											<div id="postmessage_610672" class="t_msgfont">【 “苏东剧变”二十周年的新反思（之三）】<br />
&nbsp; &nbsp;&nbsp; &nbsp;<br />
&nbsp; &nbsp;&nbsp; &nbsp;尽管中国不是共产极权的创始国度，但是中国的历史文化传统，同样非常有利于共产极权的滋生，而且中国的文化传统比俄国的文化传统更有利于共产极权的稳固：<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　中国的中央集权专制王朝历史长达两千多年，远比俄国漫长。俄国有东正教传统，中国则没有宗教传统，皇权之侧，不存在任何超越世俗权力的制约势力：俄国尚有表面独立于皇权的东正教神权，中国则连这种表面的神权都没有，中国主流社会所遵奉的所谓“儒教”，以君主为敬拜的核心，从灵到肉都是帝王的婢女，而且儒家因为对彼岸世界的回避，根本算不上宗教。俄国文化中有贵族传统，而中国文化传统中没有贵族传统，因为自西汉以后，中国的贵族阶层逐渐被消灭殆尽，而俄国的贵族力量虽弱，但“十月革命”之前的七百年里，贵族阶层毕竟一直存在。没有贵族，就少了对王（皇）权的一种制约力量；没有贵族传统，也就没有独立于世俗王（皇）权的独立人格精神，因此俄国能够产生普希金、莱蒙托夫、陀思妥耶夫斯基、托尔斯泰、克鲁泡特金、巴枯宁、普列汉诺夫等一大批独立于沙皇权力的文化和思想巨匠，中国则大量地产生依附于帝王的文人投机者，如孔丘、董仲舒、朱熹、康有为等、或者黄巢、张献忠、洪秀全等毫无建设性的造反暴民，而鲜有人格独立的文化思想巨匠…因为贵族传统的缺乏，中国精英阶层的奴性要比俄国精英阶层更为深重。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　以上可见，中国的王朝社会是比俄国社会更彻底的一元化专制社会：帝俄时期的俄国社会多少还有一点欧洲中世纪政教分权社会的外形；而中国的王朝社会则是纯粹的皇权一权专制（一元化专制）社会。也就是说，中国的社会传统是纯粹世俗的一元化专制社会传统；而俄国的社会传统则是欧洲形式的专制社会传统：同样是中央集权的社会传统，在中国的传统中，只有皇权（或世俗行政权）没有神权，也不容许任何对皇权（或中央）分权制衡的因素存在，对“功高震主”等皇权之侧的权威也很忌讳，中国的皇帝犹如上帝，只有皇帝一人神圣无比，在皇帝面前人人平等――都是奴隶；而在俄国的传统中，虽然也是皇权至上，但神权享有崇高的地位，承认贵族的荣誉和权威，沙皇虽然高贵，但也只是俄国最大的贵族而已。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　相比之下，中国的皇权（中央权力）不受任何（包括宗教力量）的制约，而俄国的皇权（中央权力）却受到东正教和贵族传统的制约，尽管这种制约比起西欧的君主小得多。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　由于共产极权是一种极端集权、极端排他性专制政权，它天然容不得任何政权之外的权威、也容不得任何分权制衡的因素，因此，中国式的纯粹皇权一权专制社会传统，要比沙俄那种带有欧洲特征的专制社会传统，与共产极权更加兼容和相通。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　由以上也可以看出：中国的历史传统要比俄国的历史传统更具专制成分；一国的民族文化来自于该国的历史传统，也就是说，中国的民族文化要比俄国的民族文化更具专制和奴性的性质。共产制度正是一种极端的专制奴役制度，因此，中国的民族文化要比俄国的民族文化与共产极权更加情投意合。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　儒家传统，是中国区别于蒙古和前欧洲共产极权国家的特殊因素，然而，这一因素的影响，却受到广泛的忽视。事实上，儒家对中国社会深刻的、持久的影响，是造成共产极权在东亚特别成功、特别顽固的重要的原因，儒家影响的因素，是世界其他地区共产国家所没有的因素。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　由于具有强大的稳定专制统治意识形态软功能，儒家自汉武帝时期开始被王朝统治者看重，在统治者的扶持下，逐渐牢牢地长期占据了中国社会的主流意识形态，形成了根深蒂固的儒家文化；随着儒家影响的扩张，自明朝开始，中国的民族文化，实际上成了一种以儒家文化为主体的文化。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　有人认为：俄国没有儒家传统，却创生了共产极权，因此共产极权的产生和存在与儒家的影响无关。这种看法是似是而非的，因为，没有儒家传统的俄国创生了共产极权，只能证明儒家传统并不是产生共产极权的必要条件，并不能证明一国的儒家传统对一国共产极权的产生和存在没有影响。　<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　不能不承认：儒家的无神倾向、追求圣王、平均主义、大同理想、歧视和抑制工商业等性质和价值观，和与自由主义精神处处相悖，却与共产极权息息相通；儒家传统的的两大特征：官本位和家长制，与共产极权的权力万能和等级专制特征，可谓是貌离神合。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　因此，一个儒家传统的社会，表面上看与共产极权势力牛头对不上马嘴，其实因为内在的两相投合性，儒家传统社会与共产极权势力，恰如插头和插座的关系，能够轻松对接、立即通电。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　自由主义是宪政民主的理论基础，正因为儒家与自由主义精神处处相悖，因此儒家传统的社会不可能自发地生长出宪政民主体制，儒家社会天然地适合专制独裁统治；迄今为止，没有一个儒家传统社会能够自发地转型为宪政社会：南韩和台湾的宪政转型是因为美国的影响和压力，香港的自由是得自于英国的统治，而缺乏西方压力的儒家社会新加坡，至今匍匐在“现代化”的李家专制王朝脚下。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　正因为儒家传统的社会与共产极权势力内在的两相投合性，因此，共产极权势力赤化中国会那样得心应手，尤其是在商业落后且闭塞的陕甘宁、华北、东北，简直如鱼得水（在江浙、上海、广东则相对困难得多）。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　与儒家作用几乎完全相反，前苏联、东欧共产国家的民族文化主体基督教（东正教）、尤其是天主教，对共产极权势力劫夺和稳固则构成顽固的障碍。因为基督教（东正教）传统的一神信仰、罪恶感、契约精神、产权观念、彼岸世界等价值观，处处对共产极权势力的劫夺和稳定构成障碍和挑战，基督教中的天主教，因为崇奉教皇为最高世俗权威，而且内部有一套等级体制，它尤其是共产极权势力树立自我权威的障碍。基督教（东正教）传统最不利于共产极权势力的影响是：它能够生发出人道主义精神，它能够从根本上动摇共产极权赖以存在的支柱――暴力镇压，这就是苏共领导人中能够产生戈尔巴乔夫那样的道德伟人，而中共却不能；东欧能够在八九民主化风潮中和平演变，而中国却不能的根本原因。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　综上所述：同为民族文化主体的儒家传统和基督教（东正教）传统对各自所在国（地区）共产极权势力的影响大不相同，在儒家传统的中国，儒家对共产极权势力的窃国和统治妨碍很小，内在的扶助作用却很大；而在基督教（东正教）传统的前苏联、前东欧国家，基督教（东正教）传统对共产极权势力却具有根深蒂固的阻碍作用。　<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　正因为这个原因，当年苏俄对东欧、中欧的赤化战略实施一度以惨败收场、一筹莫展，最后苏联是藉助“反法西斯”战争的历史机遇，通过对东欧国家的直接军事占领，才得以将共产制度移植到东欧。而同样是扶持中国的政治势力以赤化中国，却顺风顺水、几乎不用耗费一兵一卒。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　也正因为这个原因，共产主义意识形态崩溃后，前苏联、前东欧国家可以通过回归本民族的基督教（东正教）传统瓦解了共产极权统治，而在中国，回归民族传统――儒家传统，只会有强化专制独裁的作用、使共产政权重新加强；这就是共产极权在中国特别稳固、特别顽固的原因所在。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　因为深受中国儒家的影响，同为儒家传统社会的朝鲜、越南，共产极权势力同样非常稳固和顽固。在亚洲国家中，只有没有儒家传统的蒙古成功地随前苏联、东欧一道摆脱了共产极权统治。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　毛泽东之后的中共，显然看到了儒家对稳定专制独裁的巨大功用，因此逐渐抬举儒家；江泽民之后，胡锦涛在重树马列的同时，更是拼命宣扬儒家，由中宣部授意、央视包装，儒家吹鼓手于丹被捧成大红大紫的文化“明星”，有效地愚弄着新一代年轻人；毛共时代，中共以打砸抢的姿态、赤裸裸地毁坏中国民族文化，胡时代的中共，则以精心伪装成民族文化的传承者，以“弘扬民族文化”的爱国姿态，拼了老命地宣扬儒家糟粕传统、疯狂煽动文化民族主义，以此抵御西方的自由民主思潮。这，无疑是远比灌输马列意识形态更加阴险狡诈的保专制<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 法术。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　除了儒家传统对专制的巨大天然帮扶作用外，中国传统中还有一个别国所没有的糟粕传统，它同样有力地支撑着中国共产极权统治集团的存在，那就是统治集团卖国保权的政治传统，具体地说，它就是一种习惯性的做法：统治集团为了维护一己之统治，轻易出卖民族利益。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　这种卖国的政治传统，主要形成于满清时期。与中国传统的中原王朝不同，满清王朝是化外野蛮民族征服中国的结果，它自始自终带统治有强烈的殖民征服政权的性质，尽管近代以后西方列强的入侵一定程度遮盖了这一性质。因为满清政权的殖民征服性质，也因为满洲（女真）民族的落后和民族气质上的鄙劣和狭隘，清朝对中国的统治空前（当然并不绝后）的专制和阴毒，为了一族专制的利益，其对汉族和其他少数民族的戕害和压制，不择手段，无以复加、其精细恶毒程度令人发指；自始自终，享有特权地位的满人从未把自己当作中国人，而把自己当作中国的主人；直至清末，满清权贵集团仍然坚持“汉人强，满人亡的观念”、甚至公然叫嚣：“宁赠外邦，不予家奴”、“宁亡中国，也不能亡我大清”，在这种观念的指引下，满清统治者不仅不谋求国家富强，反而为了统治的稳定处处扼杀社会的活力，一心求取国家的积贫积弱。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　这样鄙劣无耻的统治心态，甚至在同为异族统治的其他国度里，都是十分罕见的，比如：诺曼征服后的英国，说法语的诺曼统治集团固然处处歧视英国主流民族――盎格鲁.撒克逊人，但对外却以英国人自居，处处为英国谋取国家利益，“宁赠外邦，不予家奴”的心态，对他们来说是不可想象的。甚至同为征服者的元朝蒙古统治者，也不曾有这样鄙劣的心态。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　在这种心态的作用下，满清统治者统治中国，自然就不仅不会维护中国的利益，反而处处以出卖被统治民族利益为能事：早于顺治时期，为了对付郑成功势力，满清统治者就勾结荷兰殖民者制造思明洲（厦门）大屠杀、乾隆帝则公开向大肆屠杀印尼华人的荷兰殖民政府特使表示感谢，感谢荷兰人大杀汉人、近代以后，面临西方列强的挑战，清统治者更是以卖国求荣求稳为能事。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　满清长达两百六十八年的殖民卖国统治，对中国的政治传统造成了极其恶劣的影响：在满清卖国政治的影响下，无论是袁世凯、段祺瑞还是孙中山、无论是蒋介石，还是毛泽东、邓小平、江泽民、胡锦涛无不习惯性地以出卖民族利益换取一己政治私利，卖国自肥、卖国以逞。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　有人认为满清以后，只是中共卖国，这并不客观，国民党同样干过不少出卖民族利益的事，只是远没有中共干得那样多、那样滥而已，例如：蒋介石国民党为了换取日本对台湾政权的支持，比毛泽东中共更早放弃了日本的战争赔款，这就是典型的为政治私利出卖民族利益的卖国行为。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　满清所开启的“宁赠外邦，不予家奴”的卖国政治歪风，在全世界几乎是绝无仅有的，因此除中共之外，包括越共在内的各国共产极权势力，都很少有主动向外国出卖国家利益的行为。斯大林对苏联人民极尽专制残暴之能事，但他却决不卖国，相反，他领导下的苏共为俄罗斯民族攫取了不少土地和利益，苏共自始至终，没有一个领导人真正干过出卖民族利益的事。即便是野蛮低级如朝鲜劳动党，都不肯出卖本国利益：金家父子不仅对日本战争赔款照单全收，还向中共国索要白头山和东北部分土地“主权”。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　满清带来的卖国政治传统，使得中共为了一党之私而出卖国家利益，远远没有别国共产极权统治者的那种心理和文化包袱，无所不用极其，特别放得开手脚。中共可以为保专制而大量出卖领土、领海、作为一个共产极权政党，中共可以委身国际资本、大肆廉价出卖国家资源、劳动力、放纵外国资本集团对本国生态环境进行毁灭性的污染和破坏…这样卑鄙无耻的事情，是这是苏共、东欧共产集团、越共甚至朝鲜劳动党都做不出来的。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　因为中国的卖国政治传统，中共敢为世界各国共产极权势力之不敢为，成功地以卖国换取以美国为首的国际资本集团的大力扶持，这是共产极权势力在中国特别稳固和顽固的另类原因。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　总之，中国特有的专制卖国文化土壤，使得共产极权在中国特别的稳固和顽固，这正是中国特别需要自由文化运动的原因；没有自由文化运动不足以尽快改善中国的文化土壤；唯有尽力去除中国文化土壤中的专制毒素，包括共产极权在内的一切专制独裁势力，才会象断了根的疹、癣一样，彻尽消失而不再复发。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 曾节明　二九年三月四日星期三中午 [博讯首发,转载请注明出处]- 支持此文作者/记者(博讯 boxun.com)</div>

							
							
							
							
