																											<div id="postmessage_130235" class="t_msgfont">层层设防 缴费高昂――外国人难入瑞士籍<br />
<a href="http://www.XINHUANET.com" target="_blank">www.XINHUANET.com</a>　　2005年02月18日 11:42:02　　来源：环球时报<br />
<br />
<br />
&nbsp; &nbsp; 本报驻瑞士特约记者 余东<br />
<br />
&nbsp; &nbsp; 瑞士经济发达、环境优美，又是永久中立国，自然成了许多移民的梦想之地。然而，在瑞士的外国移民即使有了长期居住权，也不见得能拿到那本红皮白十字封面的瑞士护照。<br />
<br />
&nbsp; &nbsp; 瑞士是外国人入籍比例最低的欧洲国家。瑞士国土面积只有4万余平方公里，人口区区 700万。很难想象，这个小小山国里竟然生活着150万外国人。瑞士联邦统计局的数字显示，外国居民中的3/4出生在瑞士或者已在瑞士生活8年以上，但只有2.1％的外国居民最终如愿以偿地拿上了瑞士护照。<br />
<br />
&nbsp; &nbsp; 如同武装中立一般，瑞士对外国移民“层层设防”。在大多数欧洲国家，入籍问题只需通过中央一级移民部门的审核和批准。在瑞士入籍，则必须依次通过三道关卡，即需分别得到联邦、州和所在城镇的批准。在联邦这一层面上，最基本也是最重要的入籍条件是：外国居民必须在瑞士住满12年，12岁至20岁的青少年在瑞士居住的时间可以一年计作两年。其他入籍条件囊括方方面面，比如是否遵守瑞士的法律、至少掌握一种官方语言、是否适应瑞士当地的生活方式等，甚至还包括要学会晚上10点以后不要大声说话，更不能放水冲抽水马桶。在入籍调查阶段，移民部门会派专人到申请者居住的社区甚至楼层，了解申请者的为人、文明程度和邻里关系等。<br />
<br />
&nbsp; &nbsp; 如果侥幸通过联邦这一关，那也不能笑得太早，因为瑞士各州和所在城镇都有权自行确定入籍标准，而且这些标准往往相差很大。比如，对居住年限的要求会因地区而异：在弗里堡州，申请人必须在州内生活3年；在下瓦尔登州，则必须住满12年，并在一个地区住满3年。此外，对申请人的经济能力也有所要求。当然，瑞士对某些人入籍网开一面。例如，瑞士公民的外国配偶可在瑞士住满5年、结婚3年之后申请瑞士国籍。如果瑞士公民及其配偶生活在其他国家，外国配偶可在结婚6年之后申请瑞士护照。<br />
<br />
&nbsp; &nbsp; 就算在瑞士熬足年头，表现良好，核准入籍，那还必须交纳一笔不菲的费用。瑞士各州的收费标准各不相同。据瑞士联邦移民局估计，入籍平均手续费相当于当地2至3个月的工资。相比之下，苏黎世州的入籍费用高于其他州，需要10万瑞士法郎（1瑞士法郎约合人民币 6.9元）。对于高收费，外国移民怨声载道。不过，瑞士议会即将颁布新的收费标准，规定只能收取基本行政手续费。<br />
<br />
&nbsp; &nbsp; 当然，也有外国人对瑞士护照不以为然。记者曾就入籍问题采访过在联合国欧洲总部、世界卫生组织等国际机构工作的一些中国籍职员，他们中的大部分至今仍保留着中国国籍，并且无意申请瑞士护照。“我觉得做一位中国公民挺好，瑞士毕竟不是我的祖国。”一位在瑞士生活近20年的国际职员这样说道。<br />
<br />
<br />
最后一段是亮点，哈哈。</div>

							
							
							
							
