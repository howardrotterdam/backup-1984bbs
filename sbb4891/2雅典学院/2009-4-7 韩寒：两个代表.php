<br />
Thank you, Mr. President.<br />
<br />
我的第二个问题是代表全世界问的。虽然我们一直在讲全球性的对策，但政治本身却是非常本土化的，正如您刚才一直优先选择美国和英国的记者提问，不过这倒没有什么。（奥巴马笑）<br />
<br />
问题是，您如何确保糟糕的本土政治不会干扰或消极的影响到正确的国际经贸往来合作？谢谢，总统先生。<br />
<br />
OBAMA: Well, those -- those are excellent questions. On -- on the first question, your American counterparts will tell you I'm terrible with those little catchphrases and sound bites. So I haven't come up with anything catchy yet, but if you have any suggestions, let me know. (LAUGHTER) I'll be happy to use them.<br />
<br />
奥巴马：你问得问题都非常精彩。关于第一个问题，你的美国同行们可以告诉你，我最不善于用关键词或短语对事务进行概括了。所以（对于中美关系）我到目前为止还没有想到用什么精辟的短语来概括。不过你要是有什么建议，不妨告诉我。（笑）我会很高兴的使用它。<br />
<br />
In terms of local politics, look, I'm the President of the United States. I'm not the President of China, I'm not the President of Japan, I'm not the President of the other participants here.&nbsp;&nbsp;And so I have a direct responsibility to my constituents to make their lives better.&nbsp;&nbsp;That's why they put me in there.&nbsp;&nbsp;That accounts for some of the questions here, about how concretely does me being here help them find a job, pay for their home, send their kids to college, live what we call the American Dream.&nbsp;&nbsp;And I will be judged by my effectiveness in meeting their needs and concerns.&nbsp;&nbsp;<br />
<br />
至于本土政治，你看，我是美国的总统，不是中国的主席，也不是日本首相，我不是参加峰会各位的首脑。我最直接的责任是让我们美国的人民生活得更好，这才是他们选举我到这个职位的目的。这也是前面几个问题中所提到的，为什么我来这里（参与20国峰会）能够帮助美国人民就业、购买住房、培养孩子上大学、实现我们所说的“美国梦”。衡量我的标准，就是要看我是否能有效地满足美国人民的需要和解决他们所关心的问题。<br />
