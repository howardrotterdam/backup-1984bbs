&nbsp; &nbsp; 如果你是芮成钢，你会怎么去想第三个问题呢？什么能比世界更大，更高，更强，更宏观呢？是代表火星还是代表宇宙呢？<br />
<br />
&nbsp; &nbsp; 你们都错了，正确的答案是，按照递进的关系，第三个问题，应该是代表党。<br />
<br />
&nbsp; &nbsp; 当你把这个思路缕清楚以后，你就有资格代表中央电视台了。</strong>&nbsp; &nbsp;<br />
<br />
RUI CHENGGANG: Rui Chenggang of China Central Television. It seems the world leaders have been talking about increasing the voice and voting rights of developing countries. I would like to ask two questions instead of just one. First one, on behalf of China...<br />
<br />
芮成钢：芮成钢,来自中国中央电视台。既然全球的领导人一直在讲要给发展中国家更多的话语权和投票权，那么我就想问两个问题而不是一个问题。第一个问题是代表中国问的..<br />
<br />
OBAMA: I may choose which one I want to answer.<br />
<br />
奥巴马：我可能只选择其中一个问题回答。<br />
<br />
(LAUGHTER)（奥巴马笑）<br />
<br />
