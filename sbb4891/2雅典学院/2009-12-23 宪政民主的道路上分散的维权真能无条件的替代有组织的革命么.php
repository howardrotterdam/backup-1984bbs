<br />
照你这个思路走下去，林冲上梁山之前，是维权人士，上了梁山，就开始参与“革命”了 -- 不过有趣的是，好象历史上很多所谓公认的革命事件，比如二月革命美国独立战争之类的，一开始都是以分散自发的“维权事件&quot;作导火索的. <br />
<br />
你这个对维权over革命的欣赏, 与其说是来自对真正民主宪政的憧憬, 不如说出自对革命失控的恐惧. 差不多就是老鼠说得&quot;分散就是力量&quot; -- 因为集中和有组织的暴力易于导致极权反过来制造一个新的怪兽吞噬个人自由 -- 一句话, &quot;躺下就不会跌倒&quot;.<br />
<br />
你这种意识形态不新鲜吧, 说白了就是老毛当年分析的小资的两面性而已 -- 一方面对自阶层缺乏政治权利的现状不满, 希望有人对当局施以颜色； 另一方面又对自下而上的革命充满恐惧, 生怕随着事态的发展自己也成了革命的对象，结果不但政治权利没拿到，就连经济权利也丢掉了 <br />
<br />
-- 于是历史上你这种人几乎毫无例外的最终倒向极右的强人政治, 正好比智利小资之于皮诺切特, 西班牙小资之于弗朗哥, 中国小资之于老蒋 -- 一方面铁腕卫道实施宪政民主给你们政治发言权(土共作不到这点, 所以你门也反土共), 另一方面希望这些铁腕人物镇压来自底层民众的民主诉求(就是所谓的避免&quot;多数暴政&quot;了) -- 民主不能太少(象土共那样, 民主就没我的份儿了), 也不能太多(太多, 泥腿子们就用选票让老子靠边站了), 象老蒋老皮老弗那样正合好.&nbsp;&nbsp;再看看俄国的现状，俄国民主了自由了，但那只是普金一伙儿及其后台老板们的民主和自由，人民只有被忽悠跟着起哄的份儿 -- 这不就是写实这种人心中最最理想的民主状态么？<br />
<br />
哈哈, 天下的美事都让你占了.<br />
