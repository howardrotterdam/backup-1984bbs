													<h2>中国劳教制度违法违宪</h2>
						
						
																											<div id="postmessage_259843" class="t_msgfont">voa<br />
<br />
记者： 斯洋<br />
香港<br />
Sep 25, 2009<br />
&nbsp; &nbsp; &nbsp; &nbsp; <br />
在中共庆祝建政60周年之际，香港星期五举办研讨会，回顾中共建政60年来各种践踏人权的行径，特别是劳动教养制度对人权的践踏。<br />
<br />
总部设在美国华盛顿的劳改基金会、劳改纪念馆和香港五七学社星期五在香港中文大学举办研讨会，从历史、政治、法律、经济、社会的角度来透视与人权背道而驰的中国劳改劳教制度，专家们特别谈到了违反中国宪法的劳教制度。<br />
<br />
*王友金：劳教制度不是法律*<br />
<br />
研讨会嘉宾，中国政法大学客座教授、香港法律学者王友金回顾了中国劳动教养制度的过去和现在，指出1957年开始实施的劳教制度只是国务院批准的行政规章、规定和办法，并不是法律。<br />
<br />
他说：“中国劳改制度是合法的，但是中国劳动教养制度是非法的，违反宪法的。”<br />
<br />
中国宪法第37条规定，中华人民共和国公民的人身自由不受侵犯。任何公民，非经人民检察院批准或者决定或者人民法院决定，并由公安机关执行，不受逮捕。<br />
<br />
*蔡耀昌：劳教不经过司法程序*<br />
<br />
香港市民支持民主爱国联合会副主席蔡耀昌指出劳教制度最大的问题是，公安部门可以不经过司法程序将公民拘留，并限制人身自由三到四年。<br />
 <br />
他说：“劳动教养完全是行政的程序， 由行政机关决定，由行政机关执行，整个过程没有机会到法院。”<br />
<br />
他还补充说，劳教制度不仅违宪，而且还违反了中国人大制定的《行政处罚法》，这个法律规定，限制人身自由的行政处罚只能由法律设定。<br />
<br />
香港法律学者王友金说，虽然2003孙志刚案后，中国废除了收容流浪人口的制度，但这只是劳教制度的一部分。劳教制度继续存在。他说，这个制度不废除，进入中国大陆的人都有可能被劳教，“因为这些规定收容的对象包罗万象，只要公安人员怀疑你有问题，就可以不通过司法程序将你送去劳教”。<br />
<br />
因发起08宪章运动目前被关押的中国异议作家刘晓波曾对劳教做过调查。他说，劳教实行50年来，中国共有350万人被劳动教养，其中大多数是政治异议者，包括民运人士、体制外的知识分子、FLG学员等。2002年，随着中国民间维权活动的高涨，许多维权人士也被劳教。刘晓波本人也曾被劳教三年。根据中国官方的数据，中国目前还有300多个劳教所，收容了26万多劳教人员。<br />
<br />
*程翔：中共建政后6500万人非正常死亡*<br />
<br />
研讨会嘉宾，新加坡海峡时报资深记者程翔则谈到了共产主义制度对人权的践踏。他发表了题为“悼念共产主义受害者”的演讲。他引用法国学者的数据说，自从人类开始共产主义试验以来，全世界共有9400万人死于这种试验。中共建政以来，中国有6500万人因为各种运动死亡。中国官方的数据是5000万人非正常死亡。程翔本人2005年因被控“间谍罪”被中国当局判刑5年，2008年获假释，是中国劳改制度的直接受害者。<br />
<br />
他说，中国政府目前在做的是抹杀中华民族的集体记忆，例如，中国当局封杀台湾作家龙应台真实再现国共内战的新作《大江大海1949》；将鲁迅的《纪念刘和珍君》从中学课本上撤走，就是不让读者对“June Fourth”事件产生追忆和类比。他希望在中共庆祝建政60周年之际，大家不要忘记了历史。“因为人类与强权的斗争就是记忆与遗忘的斗争。”<br />
<br />
*郑宇硕：中国政府推行“无民主良政”*<br />
<br />
香港城市大学教授郑宇硕则从社会现实的角度分析1989年天安门事件后中国实现民主的可能性。他认为，在中国追求民主依然是一条漫长而艰苦的道路，不管是自上而下由统治精英开创的民主改革，或是自下而上由群众发动争取民主的运动，在可见的将来，都不容易出现。他认为共产党现在推行的是“无民主的良政”。</div>

							
							
							
							
