																											<div id="postmessage_852184" class="t_msgfont">十一亲历地铁里的一幕 令我慨叹 悲哀<br />
<br />
&nbsp;&nbsp;昨天十月一 晚上乘地铁归家 因早已经过了高峰期 地铁里空空荡荡没啥人 只见车厢地面上丢着一面小国旗 就是那种在庆典的广场上群众手里拿着摇的小国旗 旅游纪念品一类 估计是谁在白天去游玩时买的 又丢在了这车厢地面上 因为现在车厢里没几个人 看上去比较醒目 <br />
&nbsp;&nbsp;我当时就有点触动 觉得这一幕也有点意味 心想如果拍下来 也算是个值得玩味的日常场景 对此情景我心里还有点窃笑 可正在我心里对这一幕玩味的时候 没想到突然出现了令我意外的状况 坐在临近座位的一个大约三四十岁的男子 突然前屈弯腰捡起了那面扔在地下的小国旗 拿起来稍微看了一下 然后很快回身将那小旗插或放在了他那排座位的椅背上 又继续坐他的车<br />
&nbsp;&nbsp;他这一举动令我小吃了一惊 随即又是一连串的内心感叹 当时车厢里也没有几个乘客 坐得稀稀拉拉 我观察那名男子 没看出什么特别之处 平常的一个城市青壮年男子 他做这件事情也不会给他带来什么好处 他为何要这样做呢 显然和他心里的价值观和其所受教育有关 <br />
&nbsp;&nbsp;很快我就到站了 随即匆匆走出车厢 但我心里却久久不能忘怀刚才车厢里发生的那一幕 在这个号称所谓国庆节的今天 有人去某广场购买小国旗 虽把它丢在了地铁车厢里 而另一个人看到后将它捡起来放到座椅背上以免其被污损 所有的这一切 所有的这些人 与我的观念差异是如此之大 而我生活在一个怎样的国家里 周围又是怎样的一些人啊 今天发生的这件事令我再次加深了对这里的了解 还有些感觉 真是一言难尽<br />
