捉奸门背后的故事：你不知道的@爱伺机摸人和@画家赵庭景美<br />
 分享到新浪微博 <br />
如果在2010年9月8号下午5点下班前你对“@赵庭景美、@陈青蓝、@爱伺机摸人”一无所知，你要么没上网，要么你是Outman。<br />
<br />
感谢反应敏捷的潇湘晨报记者曾鸣，他敏感意识到这场源自新浪，传至天涯，猫扑人肉，豆瓣文青围观将制造一个微博公众围观事件，他选择一个极为三俗的标题，写了一篇好文字，于是全国媒体疯狂转载，不明真相的群众不妨先看此文。<br />
<br />
“捉奸门”：一个捉奸故事的微博路径<br />
<br />
新浪房产频道也不含糊，立即推出《最适合陈青蓝许宇萱的小三房》，多么坚持三俗方针的媒体啊。猫扑延续了一贯的爆料风格，那就是人肉，人肉不上图不行，我们得看看这条美女蛇是谁，一看美女估计很多爷们都会原谅陈青蓝为什么说：“姐姐，今夜我不关心人类，我只想你。”，想看的先点链接看看狐狸精吧。<br />
<br />
画家赵庭景美的朋友说“旁观者永远不会把别人的痛苦当回事人肉”，其实她这个朋友是否也把朋友痛苦当痛苦谁也不知道，我只知道，当所有的人痛骂完陈青蓝时，才有爆料说两个人闹离婚已经一年了。<br />
<br />
假如事情是这样开始叙述的，我和老公已经闹离婚一年了，一直因为财产纠结没有离成，其实我们并没有经常住一起，不过我发现他最近有了相好，还上了床，我们还没离婚呢，你说他这事算不算有过错方，离婚时财产可以少判点他？估计这事就没那么容易被同情了。<br />
<br />
看来画家赵庭景美深谙要泄愤的两个人是多么强大的对手，她那支笔比鲁迅还鲁迅，她那张嘴比王小波还王小波，你想这种人，你怎么能对付？<br />
<br />
假如你不了解陈青蓝，你一定要了解爱伺机摸人是多么NB的一个女人，彪悍如老罗都不敢和她对骂，不过喜欢AV女优的朋友应该不知道她，因为她曾经提供了一份最全日本AV女优大全名单，为这事，有人写了一篇很春秋的文章《爱伺机摸人―神仙姐姐还是冰冷的美女蛇？》。<br />
<br />
看看爱伺机摸人的豆瓣日记，大部分人震撼那篇《征婚日记》，因为好懂，其实最厉害的是《格道 阴道&nbsp;&nbsp;无间道》，是用最低俗的话讲最不好说的事。<br />
<br />
她能和陈青蓝认识的，是因为他们都加入了铅笔经济研究社，铅笔经济研究社要她入伙是认可她的政治理念，这表达她政治观点的文章不太容易找，如果你有兴趣，不妨看看《在摸摸党文艺座谈会上的讲话》，假如你是左派，你不用看了，不会有共鸣，假如你是右派，建议也别看，你会发现她认为你还是形右实左。<br />
<br />
不过你可以看看《欢迎神仙姐姐加入铅笔经济研究社！》，作者布尔费墨，豆瓣账号当然是永久停用。嗯，一个人的账号被永久停用现在几乎是一种荣誉和政治资本了。<br />
<br />
可是爱伺机摸人忘了自己始终是一个女人，只有女人才懂得一个女人的死穴在哪里。<br />
<br />
文人最厉害的是笔不是逼，笔是写大事的，逼是处理私事的。笔一遇到逼，然后把家务事交给群众去解读，你说是上面那个嘴说得清还是下面那个嘴说得清？<br />
<br />
我听到群众一致表态说：我们围观，围观，再围观，继续爆料，爆料，再爆料。<br />
<br />
我建议韩寒写本小说 ，故事梗概是这样的：一个经济学家的油画家妻子，1996年开始自学油画，从此漂泊，居无定所，老公寂寞难耐在组织内认识一位极端自由主义的美女，暗生情愫，于是和妻子闹离婚，妻子心里不平衡之余，发誓一定要让奸夫淫妇不得好死，曝光于天下，一天早上，一直停留于798学习雕塑的妻子突然打开了房门，然后极为冷静的用手机发了一条微博：“早上我回家 看到床上 @北京陈青蓝 和 @爱伺机摸人 裸在我的床上”，当然她没有忘记给这条微博两个“网名”极为冷静输入了两次@符号… …<br />
<br />
我还想建议韩寒把这篇小说名字改成《这个世界，我其实没什么想和你谈谈的》，独唱团第二期还没有出，还来得及。<br />
<br />
假如女人懂男人，就应该晓得，男人如果是偷情，一般是不会选择在家的，没有人想让偷情的人知道自己太多底细。所以陈青蓝认为他在追求爱情，可是他忘了，有些人得不得爱了，就会毁灭一切。<br />
<br />
我的新浪微博评论是：画家赵庭景美的新浪微博标签是：“简单生活 画画 平和的心 读书 环保 旅行 安静 素食 798”，结果男人和别的女人一上床就变成：“围观生活 画皮 愤怒的心 读心 抓现行 咆哮 你以为老娘真是吃素食的 748（去死吧）”<br />
<br />
一次围观只会让两个自由主义者坚定去走到一起，我看到@北京陈青蓝在围脖上公然示爱，这就是明证（这里不得不佩服赵画家每次微博都不厌其烦输入@北京陈青蓝 这个符号，这不是一个早上刚刚饱受刺激的女人，这是一个昨晚就在深思熟虑的女人，我写到这里不由有点同情@北京陈青蓝，哥儿们，不容易啊）。<br />
<br />
写完这篇评论，又看了下@爱伺机摸人的新浪微博，我看到姑娘在19点24分若无其事地发了一条：“就算贫病或失忆 都争口气从旁保护你 顽强地等再过廿个十年 等整个世界换风气 历劫还是再在一起 这种坚决无人可比 看战事多悠长 亦决心打到尾心不死 @黄伟文 ”。<br />
<br />
黄伟文是个香港人，他说：“@世界最淖锩　叫太易忧椤〉我喜g@罪名”。<br />
<br />
我有点欣慰，然后我去豆瓣粉了爱伺机摸人。<br />
