																											<div id="postmessage_649440" class="t_msgfont">走进日本：令人不解五大怪事<br />
<br />
2010-05-25<br />
　　<br />
&nbsp; &nbsp; 走进日本，发现酒店马桶两侧，有两排不同功能的按钮；每家农户门口都摆有粮食、水果，可自助投币购买；吃完盒饭，自己带走垃圾；最繁华地带也不见堵车；森林密布，却进口木材制一次性筷子。日本社会的众多怪事，实在令人费解。<br />
<br />
　　前年的三月中旬，我有幸去了一趟日本，参加了一次媒体交流活动。十几天跑了五六个城市，所见所闻却不少，感悟多多。<br />
<br />
　　先说酒店。访问期间换了五六个酒店。不说酒店的早餐都是自助餐，凭房卡领取；自助餐品种十分丰富，每道茶都很清淡。单说酒店的人性化服务及设施。每个酒店，不论星级多少，设备都很齐全，都很人性化。比如说，不论单人间还是标准间，每张床上，都摆上一件全新的和式浴袍，干净舒适，免费享用。单从表面上看，房间里的一些设施，与国内的酒店差别不大，但却布局格外合理，空间利用很讲究。怪有意思的，是马桶。马桶的两侧，有两排不同颜色的按钮。刚开始，我也摸不到头绪，也许是标明不同的功能吧？我只学过点英语，却看不懂日文。在好奇心的驱使下，便向酒店服务生请教。果然猜测没错，这排按钮真的是表明不同功能：加温、冲洗、烘干、男用、女用、消毒、杀菌……不妨试试吧，果然ＯＫ！这使我想到了被媒体曝光的国内大款，披露他们别墅里装的马桶，动辄价值上百万，原先以为是媒体刻意炒作的，没想到这次我在日本酒店，亲身体验到了。<br />
<br />
　　日本的农村又是怎样呢？我们访问了一个叫“忍野八海”的农村景点，总的观感是没有什么城乡差别，反而农村更适宜居住，在西方发达国家里，富人住郊区、农村，穷人才住闹市，正好印证了这种说法。我生在农村，长在农村，一个农村走出来的土包子，心想农村有啥看头？其实不然，这个“忍野八海”的小村庄，户户门前都摆着成袋的大米、红薯、白菜、水果，还标明价格，按价出售。门前有一个投币箱，游客们挑好东西后，将钱投入箱内，可以自行取走商品。货主晚上回家时，再将剩下的物品及箱子收拾入户。据说，所有的瓜果蔬菜无化肥、无农药，都是纯天然的绿色食品。所谓的“忍野八海”，就是类似于我农村老家的鱼塘，但此塘非彼塘，水清澈见底，白的红的鲫鱼鲤鱼，欢快地穿梭着。导游介绍说，日本不食淡水鱼，只吃海鱼。农村河沟里到处都是鱼，却只当做观赏鱼。除此之外，这农村也到处干净整洁，真不可思议。<br />
<br />
　　说到整洁，不能不说说日本城市的街道。走在大街上，看不见一点垃圾，公共厕所也是一尘不染，更不用说随地吐痰了。日本大街上几乎看不到垃圾箱，你想丢一张废纸都很难。原来日本人的做法是，把垃圾放在纸袋中带回家，分类后，扔在隐蔽的垃圾房中。有一次，去丰田总部参观，中午在汽车展览馆吃盒饭，保安要我们饭后将一次性饭盒、筷子、纸巾自己带走。也许有这种氛围，让人自觉养成不乱扔垃圾的习惯吧。<br />
<br />
　　日本还是一个非常遵守秩序的国家。所到之处你不能不感叹，文明、制度、规则作用做到了最大化。论东京的街道，不见得比上海的街道宽，比北京街道直，立交桥也不多见，但感觉很通畅，很少见到堵车的现象，也很少看到值勤的交警，也没有亲眼目睹一起交通事故。原来司机们规则意识很强，都按规则行驶。一次，去东京的“银座”，大约是东京最繁华的购物街吧，人流车流当然多，是东京最稠密的地方，相当于北京的王府井、上海的南京路。但这里的人流、车流各行其道，井然有序，没有国内公共场所那种喧闹杂乱的场景。<br />
<br />
　　导游告诉我们说，日本全国一次性筷子的消耗量，差不多90%以上来自中国。可是，日本的森林覆盖率是世界上最高的国家之一，换句话说是森林富裕之国，而我们中国不到17%。尽管如此，日本却不准对国内森林进行商业性采伐，木材几乎全部依赖进口。日本对资源节约与保护，对生态环境的呵护，已经到了令人难以置信的程度。日本的国土面积不如我国很多省大，人口不到两个亿，可其综合经济实力仅次于美国，居全球第二。这说明这个国家有其独到核心的竞争力。比一比才知道，差距真的不少，不服还不行，日本有些东西确实值得我们借鉴和深思！（童安全）</div>

							
							
							
							
