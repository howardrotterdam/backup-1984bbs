													<h2>《中国通史》删除满清屠杀记录</h2>
						
						
																											<div id="postmessage_755705" class="t_msgfont">时间：2006/03/29 出处：历史研究 <br />
　　 前几日翻看新编的《中国通史》（白寿彝主编，上海 人民出版社出版六五计划重点项目），在第17、18两卷明清史部分，我惊讶的发现有关八旗军入关大肆屠杀汉族平民，推行血腥的“剃发易服”民族暴力压迫政策的章节几乎被全部删节！连著名的“扬州十日”、“嘉定三屠”也只字未提，只是在史可法一章草草地记录了史可法在小东门被多泽杀害，而之后的十日内扬州百姓被疯狂屠杀一个字也没有写。难道这一段历史真的就是空白，是有人凭空捏造出来的。这让我们的后人怎么去客观地看到历史的真实全貌？<br />
<br />
　　 这段历史已经被人有意掩盖了二百多年，是上世纪初由鲁迅等从海外找回来的，难道今天又要被继续封存另一个二百年，三百年？？<br />
<br />
　　 历史对于一个国家来说无疑是凝重而又严肃的，尤其对于我们这样一个有着漫长历史而又极其注重史学传统的国家。<br />
<br />
　　是的，历史对于中国历代统治者来说都是神圣而不可侵犯的。雄才大略、气吞万里的汉武帝刘彻能够发兵百万连续击溃匈奴，扫平楼兰，却对御史大夫司马迁要秉笔记录自己的弊政无可奈何；明朝篡位上台的永乐帝朱棣为了排除异己，登上皇位，敢于血流成河，连自己当皇帝的侄儿都不放过，但对于坚持要把这一暴政留诸青史的史官却不敢横加干涉，因为他们都深深敬畏着历史。<br />
<br />
　　再谈谈近日中央台热播的《百家讲坛》节目，有位姓阎的历史学教授在给全国观众讲述《清史》。他似乎对满清十来位皇帝是推崇万分，溢美之辞不断。一会儿说康雍乾盛世是可同汉文景之治，唐贞观之治相媲美的中国最强盛时代，一会儿又说清王朝鼎定疆域，修订《四库全书》，文治武功都达到了中国历史的最高峰。<br />
<br />
　　 在对明代相关情况进行比较的时候，他仅仅用了“皇帝懒惰昏庸，政治**黑暗”就把明朝给概括了。这对于一个历史学教授的职业操守来说，对于全国收看电视的十几亿电视观众来说都是极端不负责任的。<br />
<br />
　　我虽然不是学历史专业的，但我也知道明朝后期虽然出了几个不理事的皇帝，但也有明太祖，明城祖、明孝宗等好几位有道明君，也有“仁宣之治”、郑和下西洋等盛事伟业。并不是阎教授所称的“皇帝懒惰昏庸，政治**黑暗”；我知道明朝虽然多杀功臣，但远远也比不上满清早期屠城动辄数以十万计；我知道明朝虽然严刑酷法，但满清却也大兴文字狱，制造冤案惨案，仅吕留良一案就株连上万人；我知道满清修过《四库全书》，可其毁禁之珍贵图书资料与其编纂的数目不相上下，而且留下来的书也大都经过了删改，其对中国文化的摧残不亚于秦朝的“焚书坑儒”。<br />
<br />
　　而明朝的《永乐大典》为中国历代类书之冠；我更知道明末百姓生活在水深火热之中，又是田赋，又是“辽饷”的，但满清末年光是一个庚子赔款就达七十亿两白银之巨（条约签定的十亿两只是本钱）。莫非这些都由慈禧和光绪“劳动偿还”？毛主席说过“人民创造了历史”，难道对一个时代仅仅数几个不昏庸的皇帝进行简单的数字比较就能进行评判的吗？<br />
<br />
　　 历史是不容许这样毫无根据地夸大或者抹杀的！<br />
<br />
　　对于一个民族，一个国家来说，历史就是他们的根。这个道理在汉朝的司马迁那里就已经明白了，今天都已经进入二十一世纪，反而浑浑噩噩，请问这是时代的进步还是退步？当前社会文化界逾演逾烈的清宫戏旋风，我完全不能理解，要如此不尊重客观历史事实地鼓吹一个异族王朝，到底目的何在？<br />
<br />
　　 一、<br />
<br />
　　首先，它严重扭曲了中国人的历史观和世界观。因为我们的国家是一个汉族占总人口92%以上的多民族的大国，汉族是绝对的主体民族，是国家原动力所在。给我们的国民灌输这样一种严重错误的历史观，会严重削弱我们主体民族汉族的民族自信心。大家会认为，反正我们四千多年的历史管理得还没有人家267年好，我们再怎么努力也没有用。汉族反正就是一个没有用的民族，我们的文化，我们的历史生来就是落后和腐朽的，就象《河殇》所说那样，干脆全盘满化或者全盘西化算了。甚至只有接受人家的统治我们才有希望。<br />
<br />
　　 二、<br />
<br />
　　严重荼毒社会风气。满清一朝的文明完全是一种变态的奴才的文明。中国几千年来一直比较注重道德名节，推崇“文臣死谏，武将死战”。唐有魏征、狄仁杰，宋有岳飞、文天祥，明代也有于谦、海瑞。然而有清一朝通过空前残酷的文字狱，有思想有名节的人都被屠杀殆尽，剩下的士大夫只是一群唯唯诺诺的奴才。宣扬这样一种奴才文化使得我们的社会风气比上世纪八、九十年代要糟糕得多。<br />
<br />
　　反复、造假、无诚信，这是奴才小人的根本特征，然而很不幸的是在我们身边某些事情像电影一样轮番上演：大学校长请枪手写论文事件，假酒假奶粉事件，更严重的是这一毒瘤正开始吞噬国人的民族自尊心：某些人会为了两小钱当众给外国人下跪；某女竟然穿着军旗装给鬼子招魂。这完全就是一种奴才的卑*自虐心理在严重扭曲着人们。<br />
<br />
　　 三、<br />
<br />
　　严重毒害我们的青少年一代。我们今天的媒体似乎全被满清皇帝包围了。荧屏上整天就是辫子飞扬，各种戏说大话清宫戏不断。而且绝大多数都是对皇帝极尽吹捧，三句话离不了“皇上”，把这些寄生虫装扮得不是圣人就是贤君。这使得我们不明世事的少年儿童把它们当成了现实，从小就浸染在这样一种专制腐朽的皇权思想里，完全不知道民主和法制为何物！我们的革命先烈好不容易经过辛亥革命、新民主主义革命推翻了封建专制王权，难道这一成果要在今天毁于一旦？这对于我们未来的中国走上民主、法制的现代化道路极其不利，可以说已经到了向整个国家敲响警钟的时刻了！<br />
<br />
　　 四、<br />
<br />
　　严重败坏了中国人民的形象。每当看见我们的同胞在舞台上、各旅游景点，穿着马褂、留着猪尾（pigtail）,开着高*旗装在中外游客面前扭捏作态的时候，我就感到一种深深的耻辱。这简直就是一种自虐，故意丑化自己民族在全世界的形象。这种恶俗的民族压迫的标记一直是19世纪到20世纪华人遭受世界耻笑的原因，好不容易在辛亥革命后断了根，难道要在今天死灰复燃吗？<br />
<br />
