													<h2>方绍伟：中国政治为什么还没有崩溃？――“腐败有能论”与“加速溃败论”</h2>
						
						
																											<div id="postmessage_541075" class="t_msgfont">方绍伟：中国政治为什么还没有崩溃？――“腐败有能论”与“加速溃败论”<br />
<br />
作者：方绍伟&nbsp; &nbsp;发布时间：2010-3-31 10:38:17&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp; 八年前，社科院经济学教授樊纲写了一篇“中国经济为什么还没有崩溃?”的文章，近日，清华大学社会学教授孙立平则发表了2009年写的一篇叫“中国社会正在加速走向溃败”的文章。在网上，樊纲的文章无人问津，孙立平的文章却洛阳纸贵、转载纷纷，为什么?是社会学家的敏锐和相比之下经济学家的麻木吗?为什么对中国社会大唱悲歌的文章近来又开始特别受追捧?<br />
<br />
<br />
&nbsp; &nbsp; 　　我在另一个地方说过：中国当前的主要矛盾是群众日益增长的发泄的需要和政府不断下降的排泄的供给之间的矛盾。话音刚落，马上有人反对，认为可以说“中国当前的主要矛盾是群众日益增长的智商和官员不断下降的道德之间的矛盾”(“智商-道德矛盾论”)，却不能由此推出“发泄-排泄矛盾论”。因为“百姓通过血淋淋的比较觉醒之后主要不是为了发泄了事，而是为了摆正他们为什么需要政府和官员、以及需要政府和官员为他们提供什么样的公共服务，他们通过不懈的抗争想要回的是本属于他们的基本权利”。<br />
<br />
<br />
&nbsp; &nbsp; 　　这个评论表面无可厚非，但“发泄-排泄矛盾论”却绝不是说说而已，因为各种社会制度的差别，归根到底可以归结到解决“发泄-排泄矛盾” 的程度差别;专政制度把重点放在“控制发泄”上，民主制度则把重点放在“疏通排泄”上。“要回权利”的说法所参照的现代民主，本质上就是一种“全程解气型的制度”，它所满足的是人性中非要把气撒到无处可撒地步的需求，自治和自我选择的西方民主，满足的正是这种既有人性合理性、又有人性悲劣性的需求。合理性在于民众要一个问责的政府，悲劣性则在于过度解气会引致“负外部效应”。<br />
<br />
<br />
&nbsp; &nbsp; 　　当下的中国当然是从“控制发泄”的专政制度演变而来的，现在的问题是，经过了三十年的成绩斐然的改革之后，为什么中国人的怨气似乎不是小了而是反而更大了，以至于出现了“中国社会正在加速走向溃败”的哀叹?难道中国人日子改善之后想要更好的日子想疯了吗?<br />
<br />
<br />
&nbsp; &nbsp; 　　当然不是。福利经济学的原理告诉我们，人是一个以“相对福利”为转移的社会动物，当大家都一样穷的时候，谁人的气都不会太大，而当大家都好起来、一部分人却好得比另一部分人更多更快时，有些人的气就开始不顺了，“绝对福利改善”就开始被“相对幸福感”给压垮了。人类的“公平观念”就是来源于人的相对幸福感。在人类的情感中，“憎恨”偏偏又是比“恐惧”更加易燃易爆的危险品，这就是为什么政府再压制，所能产生的“恐惧”无论如何也抵不过 “憎恨”的爆发力。而当人们没有比网络更好的发泄工具时，“网络发泄文化”和“网络轰动效应”就进入了“中国特色”的家族。但网络发泄毕竟只是“虚拟发泄”，聪明的统治者必须提供比“虚拟发泄”更好的办法才行，连“虚拟发泄”都要压制当然是愚蠢透顶的。<br />
<br />
<br />
&nbsp; &nbsp; 　　一部分中国人现在又“憎恨”什么呢?现代政治学的解释一样直截了当：现代化点燃了人们的期望，期望急速转化成大规模的社会动员与参与，而协调这种大规模能量的制度安排往往又无法同步跟进，于是挫折和憎恨便同样急速地蔓延，以至于出现了“一方面是社会经济的空前发展，而另一方面却是社会不满的一触即发”这样的矛盾景象。这就是说，这个“憎恨”可以是“憎恨一切”，“憎恨的形式”比“憎恨的内容”更能体现“憎恨”的本质。<br />
<br />
<br />
&nbsp; &nbsp; 　　但孙立平的“社会加速溃败论”把“憎恨”的矛头指向了“权贵资本主义”。孙立平认为，“社会动荡是指严重的社会冲突会威胁政权和制度的基本框架，而社会溃败则是社会肌体的细胞坏死，机能失效”。“社会动荡的反面是社会稳定，社会溃败的反面是社会健康。……由于思路错误，‘维护稳定’的种种措施导致使社会健康化的变革无法进行，结果是进一步加剧社会的溃败”。<br />
<br />
<br />
&nbsp; &nbsp; 　　现在，“对中国社会最大的威胁可能不是社会动荡，而是社会溃败。……社会的溃败首先表现在权力的失控，腐败不过是其外在的表现。……在此之前，尽管外部约束是缺乏的，但内部约束是相对有效的;……地方性权力、部门性权力已经成为既无上面约束，又无下面监督，同时还缺少左右制衡的力量，这意味着国家权力的碎片化;官员不能负责任地进行工作，为保官升官不惜牺牲体制利益(不要说社会利益了)。……这种社会的溃败蔓延到社会生活的各个领域：潜规则盛行于社会，……社会底线失守，道德沦丧;强势利益集团已经肆无忌惮，社会生活西西里化趋势出现;利益集团的肆无忌惮，对社会公平正义造成严重侵蚀; 职业操守和职业道德的丧失是相当普遍的现象;整个社会的信息系统已经高度失真，统计数据的弄虚作假代表了体制性的对信息的扭曲。‘村骗乡，乡骗县，一直骗到国务院’，几乎可以说是比官方统计数据还可靠的现实。……社会认同和社会向心力在急剧流失。……心理的疏离是结构性疏离的反射”。<br />
<br />
<br />
&nbsp; &nbsp; 　　因此，“社会失去进行长远思维的能力。在权贵资本主义上形成的既得利益集团又过于注重眼前，既无古代帝王对子孙后代的责任，又有无贵族的超脱和超越精神。在我们的社会中，一种倾向，即短期问题夸大症与长期行为麻木症并存。对于所有眼前遇到的问题，无一不草木皆兵;而对于关乎子孙后代、社会长远发展的问题，则一概视而不见。今朝有酒今朝醉，成为一种体制性行为。在资源和环境问题上的竭泽而渔，在体制的弊病上能拖就拖”。<br />
<br />
<br />
&nbsp; &nbsp; 　　孙立平把话挑明了说：“维护既得利益是一件很累的事情，而我们社会把精力和资源过多地用到了这个地方。为了维护既得利益，不得不压制言论自由。可以想想，为了压制那些言论，我们用了多少的精力和资源?为了维护既得利益，就不得不千方百计想绕过民主这个坎儿。可以想想，为了不民主我们费了多大的劲儿，编造了多少理由和理论。为了维护既得利益，我们就不得不压制民众正当的利益表达，于是酿出了多少群体性事件，为了解决群体性事件就花费了多大的精力?为了维护既得利益，很多在其他国家行之有效的反腐败措施我们都不敢采用，为此我们不得不使用那些笨拙而无效的运动型办法，为此又浪费了多少的资源和精力?须知，要同时实现既得利益最大化和维护社会的正常运行这两个目标，是一件相当困难和费力的事情。因此，我们这个体制是很累的，管理者也是很累的，从体制到管理者的心理负担都很重。更重要的是，为了维护既得利益，我们这个社会要付出更深远的代价。<br />
<br />
<br />
&nbsp; &nbsp; 　　比如，为什么要如此大张旗鼓批普适价值?是普适价值中的什么让我们大动肝火?说穿了无非是民主自由，因为民主自由威胁既得利益。但直接批民主自由又不好听，只能拿普适价值说事了。但在信仰尽失、道德沦落的今天，连普适的价值也成了批判的对象，结果是可想而知的。但为了既得利益，又不得不如此。……改革从某种意义上说已经开始成为财富掠夺的战争，改革的共识已经基本破灭，改革的动力已经基本丧失。原因何在?就在于改革已经受制于既有的既得利益框架，即使是真正开明的改革者也无法摆脱这种制约”。<br />
<br />
<br />
&nbsp; &nbsp; 　　孙立平的结论是自由派的老生常谈，但他的分析却代表了中国自由派的较高水平。只是，尽管他从于建嵘的“社会动荡”更进一步深入到“社会溃败”问题，但他与说“没有全球化，中国就是北朝鲜;没有低人权，中国就是东欧”的秦晖一样存在明显的认识盲点。<br />
<br />
<br />
&nbsp; &nbsp; 　　孙立平到底要表达什么呢?归结起来无非两句话：“中国快完了”，“这回真的不同了”。前几年从美国高呼“中国即将崩溃”的章家敦说的也是这两句话，而且实际上，从1990年开始的崩溃论者说的都是这两句话。当高喊“狼来啦”的“小孩”多起来时，许多不满现实的人确实也跟着同感“中国快完了”，更加准确的说，这些人让毫无“恐惧”的“憎恨”化成了一个“中国快完了”的愿望，然后开始祥林嫂式地重复起来。问题是，“中国快完了”的日期为什么被一推再推呢?难道是传说中的“政党统治七十年定律”还没到期?到底是现实存在自由派所说的问题，还是自由派的愿望存在由“憎恨”引出的情绪问题?<br />
<br />
<br />
&nbsp; &nbsp; 　　实际上，我上面的大段引用表明我非常同情孙立平对中国现实的分析，但由于这种分析存在一个严重的漏洞，他所开出的“民主自由”或“多党民主”的药方就大有问题。<br />
<br />
<br />
&nbsp; &nbsp; 　　我们可以回顾一下中国的历史，每一个专制王朝的“溃败”都是什么引起的呢?历史学家o出过各种五花八门的答案，但比较共同的一点是： “统治者上层”出了问题。出什么问题呢?统治者的“德能”出了问题。历代的“治世”无一不是因为有“贤君明主”，而“乱世”则无一不是因为存在“昏君庸主”。统治者的“德能”又为什么老出问题呢?答案自然在“家族统治制度”本身。<br />
<br />
<br />
&nbsp; &nbsp; 　　家族继承能够克服个人的有限寿命，但从中国的秦始皇开始，代代相传以致千秋万代，一直只是一场春秋大梦。尽管暴力、宗教和意识形态能有助于政权的稳定，但几代人里出一个孬种，政权就危在旦夕了，这就是绝对君主制里所谓的“基因悲剧”。“基因悲剧”正是中国历史上“朝代轮回”的关键内因，也是制度近似的现代独裁制不能长久稳定的关键内因。<br />
<br />
<br />
&nbsp; &nbsp; 　　我们知道，北朝鲜有朝鲜劳动党的“金氏王朝”(金日成―金正日―金正云)，古巴有古巴共产党的“卡斯特罗王朝”(菲德尔・卡斯特罗―劳尔・卡斯特罗)。如果中国现在依然是古巴、北朝鲜那样的“家族政党制”，秦晖所说的“没有全球化，中国就是北朝鲜”就完全成立。孙立平也类似地说“改革是沾了开放的光”，可是，他忘了进一步想，开放又是沾了什么的光呢?这是个极其关键的一问，这一问当然就问出了毛泽东到邓小平的大过渡。这个大过渡的本质是什么呢?还是那个“贤君明主”问题吗?既是，又不是。<br />
<br />
<br />
&nbsp; &nbsp; 　　自由派是不喜欢“贤君明主”的，自由派要的是“制度”或“多党轮流坐庄”的制度。可如果你没有当年中共推翻国民党的能耐，你现在就只能盯着中共的制度。问题就出在这，孙立平象秦晖一样接近于认为“没有全球化，中国就是北朝鲜”，就是说，中国表面上不是“家族政党制”，但实质上还是“一党独裁制”。认识的差别就出在这，自由派的“制度主义”出了“逻辑不彻底”的大问题。<br />
<br />
<br />
&nbsp; &nbsp; 　　为什么这么说呢?因为“毛邓大过渡”的本质不是别的，是“一党独裁制”或“现代独裁制”向“一党立宪制”的过渡。这个“立宪”现在当然还没有走到“分权制衡”的地步，但中共的“家族独裁”性质本来就很弱，毛泽东时代的“个人独裁”性质则由于邓小平的“制度化理念”而发生质变，并在 2002-2004年的“江胡交接”中完全实现。自由派不是看不见中共的“制度变迁”，而是“视而不见”这种变迁。这种变迁的意义当然不仅仅是中共历史上 “和平与制度化”的权力交接，而且是这种交接所包含的“限任化、非私人化、全民开放化、制度化”的政党法治变革。这个“政党四化发展”就是“一党立宪制” 的开始，其中的“限任化”包含了明确的年龄、任期、年限等方面的制度化内容，具有明显的“立宪”性质。<br />
<br />
<br />
&nbsp; &nbsp; 　　当然，这种立宪不是自由派所希望的多党立宪，其实质仍是“一党统治”，但在学术上我们没必要因为某种“憎恨情绪”而否定中共的立宪发展。要特别注意的是，这个“一党立宪”不是我希望或我主张的“一党立宪”，而是我观察到的正在发生的“一党立宪”。我这里只涉及“事实判断”，不涉及“价值判断”，更不涉及“价值主张”。<br />
<br />
<br />
&nbsp; &nbsp; 　　中共的“政党四化发展”一方面克服了置苏共模式于死地的“勃列日涅夫陷阱”，另一方面又克服了中国历史上家族统治的“基因悲剧”，“统治者上层”出问题的“历史恶性循环”最终被“一党立宪制”打破了。还要注意的是，中共的“阶梯式接班制度”与“条块代表式的集体领导”和“军委主席制度” 巧妙地交织在一起，它既能有效地选拔出较具“德能”的领袖，也有利于防止出现“戈尔巴乔夫”式的人物。当然，中共的新体制能否真的占尽“优越性”仍有待时间的检验。但有一点是肯定的，新体制使中央的制度权威更大更巩固了，1994年的分税制则进一步强化了中央的财力，地方如若有人胆敢再跟中央叫板，陈良宇就是榜样。<br />
<br />
<br />
&nbsp; &nbsp; 　　中共的新体制的确也强化了中国传统的“官本位”，政府费用占国民收入居高不下的比例就很好地表明了这一点。可孙立平所说的改革之前， “尽管外部约束是缺乏的，但内部约束是相对有效的”确实是判断有误。事实是，中共的纪检和司法在不断地强化，内部约束依然是相对有效的。“权贵资本主义” 的横行，实际上反映的并不完全是中共“内部约束的有效性”，它主要反映的是中共新体制的“政治单一制”和“财政联邦制”特征上的“内在制度矛盾”。“内部约束失效”的判断是对“中央制度权威”的误读，更是对中国“政治经济内在矛盾”的误读。<br />
<br />
<br />
&nbsp; &nbsp; 　　这个“双重误读”的结论必然是，中国现政府已经同它的历史前驱一样“腐败无能”、“病入膏肓”了，所以就是“中国快完了”，“这回真的不同了”。我观察到的现实却是，中国现政府不是“腐败无能”，而是“腐败有能”，而且中央高层的腐败比地方和部委要低得多。中央为什么会比地方部委更廉洁呢?中国的“政治经济内在矛盾”又是什么呢。<br />
<br />
<br />
&nbsp; &nbsp; 　　中央的相对廉洁我已经在“产权国家论”里作了分析(涉及“产权责任”和“强核心效应”)，这里不再重复，下面只概要地分析一下中国的“政治经济内在矛盾”。<br />
<br />
<br />
&nbsp; &nbsp; 　　首先，我们应该把“权力市场化”的“权贵资本主义”放在一种国际视野之中。这里要借用美国政治学家迈克尔・约翰斯顿于1997年提出、并于2004年在《腐败症候群》一书和2009年的新作中展开运用的“精英-机会分析框架”(我在别的场合从宏观和微观两方面延伸了这个“中观性质”的框架)。这个框架涉及“精英可接触性”和“政治经济机会”的分析，它认为“精英独立性-可接触性”和“政治-经济机会”一但出现失衡，“权贵资本主义”就会产生。<br />
<br />
<br />
&nbsp; &nbsp; 　　在西方发达国家(美国)，精英的可接触性超过其独立性、经济机会多于政治机会;在这种“权势市场型”中，私人利益集团力量强大，政治精英很容易受到合法捐赠或非法贿赂的双重腐蚀。在新兴民主国家中(印度)，精英的可接触性高，政治机会的充裕，但经济机会却相对不足，这使得精英的地位易受外力和裙带关系影响，“分权切割式的寻租”就加剧了精英的短期行为和腐败倾向。在传统专制国家(缅甸)，精英垄断了政治经济竞争，裙带关系盛行，腐败呈现出“独裁式寻租”的倾向。<br />
<br />
<br />
&nbsp; &nbsp; 　　中国的情况有所不同，政治精英一般通过控制政治门路来获得经济利益，充裕的经济机会又使竞争政治权力的利益集团呈现多元化的趋势，这是一种以“官僚权贵”为特点的“集权主导型寻租”。“不让有钱的人有势，不让有权的人捞钱”恰恰不是这种类型的制度逻辑。<br />
<br />
<br />
&nbsp; &nbsp; 　　如果更进一步分析，我们会发现中国的“权贵资本主义”反映的是“政治单一制”和“财政联邦制”结合下的“内在制度矛盾”。这个矛盾可以用一句话来概括：“又要马儿跑，又要马儿不吃草”。<br />
<br />
<br />
&nbsp; &nbsp; 　　中央要集中和扩大财权，却对地方预算外和非预算项目严加控制，这当然是实现不了的。中央要在民生开支上甩包袱，又对地方分权让权不足，这当然也是实现不了的。中央要信任地方官员“为官一方”，又要对地方官员进行严密的控制，这是鱼和熊掌的两难。中央要地方发展经济，又不给地方的积极性和灵活性，这又是鱼和熊掌的两难。中央要改革开放多元发展，又要解决多元化中的信息不对称问题(隐蔽信息与隐蔽行为);中央依靠经济绩效和政治关系驱动的 “政治升迁”，又要地方官员避免地方主义和短期行为;中央要地方保证社会稳定，又在土地产权、人员流动、问责权利等方面控制过紧;中央要地方官员保持政治忠诚，又要限制地方官员中“关系型文化”的滥用;等等等等。<br />
<br />
<br />
&nbsp; &nbsp; 　　在这些“跑和吃草”的矛盾中，中央只能由着地方“既跑又吃草”，然后端着枪“专打出头鸟”，以解决信息不对称带来的高“交易费用”问题。表面看上去是“内部约束失效”，实际上却是省费用减负荷的招数。腐败不利于发展，但腐败有时确实是发展的润滑剂。但是，更深一层的矛盾是，“一党立宪制”对“分权制衡”和“民主问责”所包含的“多党自由化”不确定后果充满了顾虑，而“问责制衡”到“多党自由化”也确实只有一步之遥。<br />
<br />
<br />
&nbsp; &nbsp; 　　这种“僵局”是否存在“可持续性”呢?当然存在，一方面是因为世界各种类型的“权贵资本主义”都使各国“带病运行”，无病运行的资本主义本身根本就不存在，“多党自由化”只能改变“权贵资本主义”的类型，却根本无法彻底消灭“权贵资本主义”本身;另一方面是，中共还有“摸着石头过河”的 “邓氏高招”，“问责制衡”依然是中共高层在“内部约束”上信息严重不对称时“不得不”使用的办法;另外，中共的合法性资源并没有因少数自由派的鼓噪而彻底丧失，中央的制度权威也没有因少数自由派的“视而不见”而下降。还有，中共对知识分子的“分化利用”实际上也是卓有成效的，多少人进入了类似“法案起草”的工作岗位之后，都由衷地提高了参与感、荣誉感和更加重要的“政府不易”之感。<br />
<br />
<br />
&nbsp; &nbsp; 　　不可否认，中国的问题非常多，你如果注意观察对比，你还会发现中国人的所谓“劣根性”也特别明显。“在中国大概最不难找到的东西，就是问题。而且中国的问题还特别复杂”(樊纲)。这是因为中国正处于一个“十足的风暴”之中：国内方面是从计划经济向市场经济的转变，从传统农业社会向现代工业社会的转变，从专制的“家族政治”向立宪的“政党政治”的转变;国际方面是新一轮的全球化浪潮，产业革命向信息化的转变，以及各种文明更加凸现自身特性的世界文化的大融合。<br />
<br />
<br />
&nbsp; &nbsp; 　　中国现存的具体问题包括：权贵腐败严重;银行系统和国有企业效率低下;失业和贫富差距扩大;侵犯知识产权和假冒伪劣泛滥;住房医疗教育危机;社会风气低下;犯罪活动猖獗;群体事件和社会动荡加剧;环境污染严重;易受国际经济震荡的损害等等。如果你愿意，这个清单还可以不断列下去(如矿难不断)。<br />
<br />
<br />
&nbsp; &nbsp; 　　如果你只想看坏消息，只关注中国坏的一面，你会非常逻辑地从你想看到的坏消息中得出“一切不可持续”和“中国快要崩溃”的结论，正如你只关注中国好的一面(如GDP跃居世界第二，人均收入从落后到远远超过印度，城市化规模和速度史无前例，等等)，就会非常逻辑地从你想看到的好消息中得出 “中国正在崛起”的结论一样。<br />
<br />
<br />
&nbsp; &nbsp; 　　可是，在崩溃论预言一浪高于一浪时，中国政府依然还没有崩溃，就一定是另有原因。而这个原因一定与中国政府做对了什么至关重要的事情有关。你可以说这件事情就是“改革开放”、“发展经济”、“关注民生”等等，我认为这个至关重要的事情不是别的，是“一党立宪”。“一党立宪”是三十年来中共做的使中国政府依然还没有崩溃的最重要的一件事。<br />
<br />
<br />
&nbsp; &nbsp; 　　人是一个“相对福利化”的社会动物，也是一个“兴奋边际化”的社会动物。“兴奋边际化”说的是，人是健忘的，人的兴奋点是会随着“刺激循环周期”而推移的，今天看上去会死人出大事的“权贵资本主义”，明天就会变成少见多怪的“权贵资本主义”的一种;兴奋点天天有，每天都会有够刺激的事发生。“多党自由化”的愿望是可以保存，但“权贵资本主义”的发泄却是自由派的“憎恨”使然。它注定是老调重弹，与前几年章家敦的“中国崩溃论”不可能有本质的区别，而且这种论调今后必定还会再现，不同之处只在于，当他们又说出“这回真的不一样了”时，那个“不一样”还是那个一样的耸人听闻但却情景置换的故事。<br />
<br />
<br />
&nbsp; &nbsp; 　　太阳底下不会有什么新鲜事，面对这样一些老生常谈，我们可以时不时地扪心自问：如果崩溃论者施放的四面楚歌确非危言耸听，如果崩溃论者绘声绘色的“燃油之湖”也非杯弓蛇影，那么，从1990年到20年后的今天，中国政治为什么还没有崩溃?<br />
<br />
<br />
&nbsp; &nbsp; 　　老百姓确实容易经常被“憎恨”烧昏头脑，但如果知识分子也经常被“憎恨”烧昏头脑的话，中国可真的“快完了”。那些动不动就“中国崩溃”的人，当心把自己的精神弄崩溃了。新左派的愿望可能祸国，但自由派的愿望则足以殃民。</div>

							
							
							
							
