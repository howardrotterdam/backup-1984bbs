													<h2>易苇航：方绍伟：人性的、太人性的……</h2>
						
						
																											<div id="postmessage_541758" class="t_msgfont">《多党民主是个坏东西》之后，方绍伟先生应天则所所长盛洪先生所邀，发表《方绍伟：幻想化解不了多党民主的悲剧》一篇论述，进一步阐述多党民主是坏东西。从方绍伟先生这两篇文章内容看，方绍伟先生在民主基本认识上正在故意把民主问题引向一个附加先定词的危险境地。<br />
从多党民主出发，探讨民主问题与多党制败德一样，将把人们引向一个败德的讨论。<br />
方绍伟先生不是从民主的角度叙述多党民主，而是从多党制民主的角度对民主加以界定。这种逻辑上的本末倒置，并不符合方绍伟先生的学识背景，我真的怀疑他是故意所为。<br />
像所探讨所有的文明观念一样，要探讨多党民主或一党民主，对民主的认识是一个基本前提。<br />
1、民主不是建立在人性的假设之上，民主建立在人权的共享之上<br />
在“多党民主是个坏东西”这个访谈中，方绍伟先生说：<br />
民主的问题不是“确有诸多缺陷”的问题。是从人类文明发展史透视民主的“未为人知的本质”的<br />
基础上，我才得出“民主是个坏东西”这个结论的。<br />
在“幻想化解不了多党民主的悲剧”一文中方绍伟先生继续阐述说：<br />
这里要讨论的民主的本来面目，是一般理论常识之外的、大家都还没有看到的那个民主。这里的讨论强调“实证至上”，我关心的是“人性和人性之上的民主制度”到底“是什么”。<br />
方绍伟先生说，他要“讨论的民主的本来面目，是一般理论常识之外的、大家都还没有看到的那个民主”，在方绍伟先生看来，这个民主的本来面目是“人性和人性之上的民主制度”，民主是“未为人知的本质”。<br />
原来，方绍伟先生在中国一些大学走了一圈，它所谈的民主还是大家没看到的民主。也许，由于某种未为人知的原因，方绍伟先生没有看到，然而我看到了。我就从方绍伟先生身上真切的看到：<br />
方绍伟先生在中国大骂包括美国在内的国家的多党制民主是坏东西，否定了美国的多党制度。尽管他涉嫌诽谤美国政治制度，他安然的从中国飞回芝加哥，在他的宽敞明亮的办公室里继续做他的芝加哥“制度经济研究中心”主任，继续写他的《多党民主的悲剧》！而美国没有什么人公诉他，审判他，判他颠覆国家政权。<br />
方绍伟先生享受着这个民主，可以无所恐惧的否定他所在国家的多党民主制度。它无需探讨民主在“一般常识理论”之内的意义，所以他把目光转向一般理论常识之外的、大家都还没有看到的那个民主。<br />
方绍伟先生，你有学术自由权力按你的理解去探讨。你的国家不会干涉你，我也不会干涉你。<br />
在国内外政治学领域，的确有很多学者在探讨民主的深层次根源。有一种理论把民主的根源最终引向基督教，从而把民主的理解引向人性。方绍伟先生不过以此为出发点，从政治伦理学关于民主的人性假设来审视民主。民主的人性基础也许是一个漂亮或不漂亮的政治伦理学理论，它进入方绍伟先生学术视野，是方先生探讨一般理论常识之外的、大家都还没有看到的那个民主的民主本来面目的批判性起点。但是，我在这里要告诉方绍伟先生，这不过是关于民主观念假设中的一种。你可以批判它，但是你要明白你批判的仅仅是民主的人性假设的观念，你无法籍此完成对民主本身的批判。<br />
让政治伦理学家去探讨民主的观念假设问题吧，让方绍伟先生去批判民主的人性假设的政治伦理学观念吧，那是他的自由。<br />
在我的字典里（在中国更多追求民主的人的民主字典里），民主无需建立在任何伦理学的假设上。正如民主未经政治伦理学的实证就建立起来一样。<br />
政治伦理学关于民主的人性假设，仅仅是对民主的一种后天解释，在这种解释出现之前民主造就成为民主的那个样子。按着马克思的解释，人类早在氏族时代就在氏族组织中实践着民主原则！<br />
对追求民主――而不是把民主作为学术研究对象――的人们来说，民主没有假设，或者说民主只有一个假设，这个假设不是人性这种虚无缥缈的东西，而是实实在在的东西，她的名字叫做：<br />
人权！<br />
在原始共产主义时代还是在雅典时代，民主已经成为社会实践，像所有制度在最初阶段存在不成熟因素一样，尽管在这两个时代民主存在缺陷，人们还没有发明“人权”这个词来对抗民主可能的不幸，但民主无疑就是指向越来越多人享有越来越多的人权。经由民主――这种权力得以实现的组织形式――人的天赋权力得以实现。<br />
这个人权，这个民主的唯一的不是假设的假设，今天他就明明白白地写在这样一些包括中国在内的众多国家签字画押的国际法文书里：《联合国家宣言》、《联合国宪章》、《世界人权宣言》、《公民权利与政治权利国际公约》、《经济、社会、文化权利国际公约》、《中华人民共和国宪法》等等。<br />
这些法律文件揭示的人权，不是未为人知的本质，相反，每个人都能没有障碍的知道他，都能够在他的社会生活里把每一条具体的人权对应与生活本身。<br />
这包括：<br />
第一条 人人生而自由,在尊严和权利上一律平等。他们赋有理性和良心,并应以兄弟关系的精神相对待。<br />
第二条 人人有资格享有本宣言所载的一切权利和自由,不分种族、肤色、性别、语言、宗教、政治或其他见解、国籍或社会出身、财产、出生或其他身分等任何区别。<br />
并且不得因一人所属的国家或领土的政治的、行政的或者国际的地位之不同而有所区别,无论该领土是独立领土、托管领土、非自治领土或者处于其他任何主权受限制的情况之下。<br />
第三条 人人有权享有生命、自由和人身安全。<br />
………………………<br />
第六条 人人在任何地方有权被承认在法律前的人格。<br />
第七条 法律之前人人平等,并有权享受法律的平等保护,不受任何歧视。人人有权享受平等保护,以免受违反本宣言的任何歧视行为以及煽动这种歧视的任何行为之害。<br />
第九条 何人不得加以任意逮捕、拘禁或放逐。<br />
………………………<br />
所以，方绍伟先生，回到你的美国芝加哥做你的学术研究吧，研究你的一般理论常识之外的、大家都还没有看到的那个民主吧。我们，这些多少还有点马克思主义思想的中国人，我们相信马克思的一个基本信条：<br />
人不是要解释世界，而要改变世界！<br />
2、方绍伟先生最终重新回到他所否定的人性假设的政治基础<br />
在“多党民主是个坏东西”这个访谈中，方绍伟先生说：<br />
民主的问题不是“确有诸多缺陷”的问题。是从人类文明发展史透视民主的“未为人知的本质”的<br />
基础上，我才得出“民主是个坏东西”这个结论的。<br />
在“幻想化解不了多党民主的悲剧”一文中方绍伟先生继续阐述说：<br />
这里要讨论的民主的本来面目，是一般理论常识之外的、大家都还没有看到的那个民主。这里的讨论强调“实证至上”，我关心的是“人性和人性之上的民主制度”到底“是什么”。<br />
正是基于对人性的怀疑，方绍伟先生否定了那种建立在人性基础上的民主的假设，从而他以为他就完成了对民主的否定。<br />
不幸的是，还有不幸的是，在方绍伟先生的这个阐述里存在两个不幸。<br />
第一个不幸是，在政治伦理学界，存在着民主前提的多种假设，方绍伟先生即使否定了那种建立在人性基础上的民主假设，也不过仅仅否定了那种建立在人性基础上的民主假设。其他关于民主的前提假设都安然无恙。因此，方绍伟先生即使否定了那种建立在人性基础上的民主假设，也无法否定其他所有关于民主前提性条件的假设，更否定不了民主本身！<br />
不幸还在于，方绍伟先生以为自己做到了。就像他看到一个杀人场面，他闭上眼睛，说那把刀只是一个可能杀人的错误假设，由此他告诉自己这个杀人的场面是不存在的。<br />
方绍伟先生的第二个不幸，也是最巨大的不幸。<br />
他说，他要透视民主的“未为人知的本质”，他说他关心的是“人性和人性之上的民主制度”到底“是什么”。<br />
他从西方经济学关于“经济人”的假设出发，向我们揭示出人的自私自利的人性本质。他从经济学的“公地悲剧”先知性的发现了政治学的“公地悲剧”。他说，政治自由――多党民主――中的个人，由于人性的自私自利的本质，必将导致政治自由“公地悲剧”的发生。<br />
方绍伟先生列举了政治自由的种种可能的罪恶。没问题，政治自由下的多党民主的恶不是方绍伟先生首先发现的，方绍伟先生转述的这些政治自由民主之恶在中国稍有历史常识的人都知道，一个典型的例子就是法西斯在德国最初取得政权的事实。方绍伟先生所列举的民主之恶都没有我举出这个例子更能够说明民主之恶。既然我看到了比方绍伟先生看到的民主之恶更恶的民主之恶，我为什么还要鼓吹民主？<br />
这是因为，民主关于权力的假设之一就是权力本身就是恶！民主就是要通过权力的制衡、通过民主保护每一个人的人权让那个恶的权力不作恶。<br />
权力制衡不是什么秘密，也不是什么信仰，它只是人类文明发明出来的对抗权力可能作恶的一种政治形式。<br />
真正的信仰在于人权。<br />
是基督教经由宗教改革运动去除了神的身影，让人在神的阴影里站立起来。而后经由启蒙运动让人在神和人的双重名义下发现了人的权力本身。并且把它写进人人可懂的法律文书。保护人权成为一个目标。<br />
人权，天赋人权不可的侵犯！这是民主的宇宙中心。德国的民主曾经诞生了希特勒法西斯这个魔王，诚然是民主历史上的污点。所谓“民主的多数人”保证诚然要为此负责，然而，更需要为此负责的，是那个国家、那个国家的人民普遍的人权信仰的缺失。<br />
那时的德国人不把犹太人看作是于他们享有同样人权的人，他们集体迫害犹太人。<br />
正是基于此，在大西洋宪章以及以后的反纳粹政治宣言里，在反法西斯国家通过的政治文件里，不仅强调“他们尊重所有民族选择他们愿意生活于其下的政府形式之权利；他们希望看到曾经被武力剥夺其主权及自治权的民族，重新获得主权与自治”，他们更强调“在纳粹暴政被最後消灭之後，他们希望建立和平，使所有国家能够在它们境内安然自存，并保障所有地方的所有人在免于恐惧和不虞匮乏的自由中，安度他们的一生。”<br />
“保障所有地方的所有人在免于恐惧和不虞匮乏的自由中，安度他们的一生。”<br />
这样，世界不同的国家在人权问题上达成了共识。这种共识几乎是二战后所有国家政治的出发点。尽管曾经出现过所谓“资产阶级民主”与“无产阶级民主”的激烈争论。<br />
制度的缺陷只存在程度上的差别。正如有人所说，民主是迄今人类“最不坏”的制度。<br />
然而，方绍伟先生死死抓住民主的缺陷，就像那个印度人死死抓住大象的尾巴，从而说大象不过是一根绳子而已。<br />
这还不是方绍伟先生第二个巨大不幸的全部。<br />
方绍伟先生从将“未可知”人性作为假设的政治伦理学的民主――而不是从基于人权的民主――出发，批判了民主的不确定性。显然他认为民主的人性假设是靠不住的，也就是说人性是不可靠的，他认为唯一可靠的就是产权。可能他忘了，马克思论述过的黑心的资本家――那个对工厂有着明晰产权的个人――如何利用剩余价值剥削工人的基本事实。产权的拥有者不是天然的无产权者的保护者。没有关系，目前看来这并没有影响到方绍伟先生关于产权国家的假设。<br />
然而，具有讽刺意味的是，方绍伟先生认为以人性为基础的民主假设并不可靠，只有以产权为基础的一党制立宪才可靠。为什么以产权为基础的一党制立宪才可靠呢？方绍伟先生告诉《汕头都市报》执行主编王永章先生，这是因为明晰的产权更容易催生统治者可靠的“政治责任心”。姑且不说，这个政治责任心是面向产权本身的政治责任心，还是面向那个国家生活的有着天赋人权本质的人的责任心。他说产权国家因为具有明晰的产权因而更容易催生统治者可靠的“政治责任心”，他就由此重新回到了他前边否定的人性的基础。原来，他还是以可靠的“政治责任心”这种人性的东西作为产权国家合理存在的论证证据。<br />
那么方绍伟先生，可靠的“政治责任心”，这种人性的东西，这种“未为人知的本质”，如何克服了民主语境下的不可靠性，完成华丽转身，从而成为一党立宪可以信赖的基础了呢？</div>

							
							
							
							
