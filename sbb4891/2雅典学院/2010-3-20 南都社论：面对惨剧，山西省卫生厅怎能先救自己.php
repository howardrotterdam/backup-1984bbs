 <br />
(恳请转发）&nbsp;&nbsp;<br />
 <br />
<br />
&nbsp; &nbsp; 有媒体近日报道称：山西近百名儿童注射疫苗后或死或残，或引发各种后遗症，问题疫苗成为最大怀疑对象。报道还披露山西省疾控中心和疑似卫生部部属企业联手垄断山西疫苗市场，在贴标签时，导致大量疫苗长时间暴露在常温下，有可能造成失效。山西省卫生厅已断然否认报道所反映的问题。卫生部要求山西尽快报告接种异常反应监测报告，但当地称一切正常。<br />
<br />
&nbsp; &nbsp; 山西疫苗乱象在2007年就有公开报道。记者突破各种采访阻挠，揭示了权力租赁的实情，清晰展示山西省卫生厅与恶性垄断企业间的利益链条：一家私人小公司托管了山西疫苗市场，既利用疾控中心的权力调控及引导市场，又以疫苗批发企业的身份高价倾销疫苗。山西省疾控中心是台前幕后的推手，提供政策支持，帮助制造接种疫苗的机会，收取合作分成。在政府部门的荫庇下，这家注册资本只有50万元的私人公司在22个月内从山西获利1亿元。当年也发生过疫苗接种事故，山西省卫生厅赔偿了一起。如今，近百名儿童接种疫苗后发生的惨剧将山西疫苗问题重新拉回公众视野，但除了这些被媒体发掘出来的受害者，一切都与从前一样。面对受害儿童的死亡或伤残，当地卫生部门的冷漠一如既往。<br />
<br />
&nbsp; &nbsp; 山西省卫生厅在第一时间否认其当为群体性的惨剧负责，声称抽检疫苗样品全部合格，药监局对疫苗注射异常反应有正常监测和调查。显然，山西省卫生厅的第一反应不是细究事件本身，不是澄清权钱交易嫌疑，而是急忙撇清自身干系。与此同时，为当地卫生部门辩解的新闻报道多了起来，让人怀疑政府部门不去关心受害人的死亡和病痛，却开始展开多样的媒体公关活动。<br />
<br />
&nbsp; &nbsp; 有一种看法是，即使山西以排他性协议外包二级疫苗的买卖，可缺乏证据证明这与百名儿童的或死或残具有因果关系。问题是，要推翻两者之间的因果联系，首先最该举证的是山西省卫生厅，而不是公众。另外，如果疫苗市场的垄断足够稳固，利益共同体有能力不承认大众找到的证据。退一步讲，当地卫生部门需要正面回应疫苗惨剧，找出致病答案，而不是卖弄官僚习气。<br />
<br />
&nbsp; &nbsp; 问责归于无力，这在三年前就已发生过。当时揭露山西疫苗利益勾结的报道很快消失不见，代之以所谓辟谣的宣传稿。这一切都让人觉得某种黑暗势力可以吞噬任何质疑，哪怕是用生命凝结的追问。这次涌现近百起伤亡案例，可以带来改观吗？至少到目前为止，卫生部门的说辞显示与从前并无二致。<br />
<br />
&nbsp; &nbsp; 近百名儿童或许又将成为牺牲品，在权力和金钱绝对掌控局势的情势下，谈殷鉴教训过于奢侈。可是假如任由利益集团再次全身而退，又怎能保证不会出现后继的惨剧？三年多来，对山西疫苗腐败案的举报始终未断，尽管证据翔实，却不见任何追究，无人为此负责。到底是怎样的势力才能将一省的疫苗市场稳稳纳入囊中？对遭难儿童的同情有多深，对此间的不忿就有多深。<br />
<br />
&nbsp; &nbsp; 如果我们不能保护孩子，问题疫苗就并不是最可怕的。其实，家长正因为信任政府对公共卫生的承诺才带孩子去注射疫苗，哪知疫苗早已成为权力交易中的可疑之物。当地卫生部门重复着言之凿凿的辩词，匆忙来为自身解脱，可人们只想知道一个答案：你们真的尽责了吗？当孩子一次次成为权力自私自利的牺牲品，任何辩解都是可耻的，而谎言只会制造新的牺牲。<br />
