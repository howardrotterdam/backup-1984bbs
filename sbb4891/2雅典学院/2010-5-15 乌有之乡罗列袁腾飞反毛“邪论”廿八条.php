																											<div id="postmessage_616576" class="t_msgfont">袁腾飞邪论28条登峰造极<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;1.毛这个人多二啊，治国无方，扰民有术，狗屁不懂的东西。毛肯定是不懂马克思主义的，他又不懂英文，不懂德文，俄语也不懂，你看过马克思主义的著作吗？没有。他看得最多的是《资治通鉴》，怎么整人啊，怎么玩人啊，中国古代的帝王的阴谋之术，他这个玩得炉火纯青。&nbsp;&nbsp;<br />
&nbsp; &nbsp; 2.毛别说在中国历史上，在世界历史上都是少见的暴君独裁者。<br />
<br />
&nbsp; &nbsp; 3.毛这个人特神在什么程度呢？他连小学四年级的自然科学知识都不具备。据无耻文人们说，他是个诗人，而且还是浪漫主义诗人，这种人最可怕了，这种人一般具有神经病的气质。<br />
<br />
&nbsp; &nbsp; 4.毛想当然，张嘴就来，这种人在国际上有个外号叫IBM，international big mouse,国际大嘴巴，张嘴就来，想说什么叫说什么，不负任何责任。<br />
<br />
&nbsp; &nbsp; 5.毛的诗也只能归入打油诗一类。<br />
<br />
&nbsp; &nbsp; 6.哪个读过书的人，如果认为他伟大，那你的书就读到狗肚子里去了。<br />
<br />
&nbsp; &nbsp; 7.我们的教科书按照中国古代史书的体例属于秽史。你问我这教科书有多少东西是真的？低于5%。你考完以后赶快把它烧了，搁家里一天都脏你屋子，我从来不往家带这书，扔车里，能不看尽量不看，纯粹是胡说八道。<br />
<br />
&nbsp; &nbsp; 8.你现在有炉子了，你也有燃料了，你原料哪里来啊？你拿什么炼钢啊？你把毛填进去，出来钢，不可能啊。<br />
<br />
&nbsp; &nbsp; 9.《人民日报》你敢看吗？那根本就不是报纸，那是宣传。新华社的稿件，世界各国都不予采纳，只有两个国家除外，朝鲜和伊朗，我们跟地球村的无赖关系很好的。<br />
<br />
&nbsp; &nbsp; 10.我不怀疑社会主义中国什么邪事都能干得出来，在毛统治下什么事情都能发生。<br />
<br />
&nbsp; &nbsp; 11.（大跃进）毛倒行逆施。。。（庐山会议）毛在会上胡喷。。。<br />
<br />
&nbsp; &nbsp; 12.毛已经不是皇上的问题了，他变成了教主了。所有的独裁者都是心里不健康的，而且都是弱智，没有一个独裁者能做出合理的判断来，二十世纪三大暴君：希特勒、斯大林、毛，全是这个德性，是吧，三大法西斯恶魔都是这个德性。<br />
<br />
&nbsp; &nbsp; 13.三年灾害完全是人灾，毛灾，保守估计活活饿死两千万人，现在有国外学者统计说是四千三百万。&nbsp;&nbsp;<br />
&nbsp; &nbsp; 14.毛纪念堂不是不可以去，你可以去，但是你要记住那是什么？那是靖国神社，里面供奉的是一个双手沾满人民鲜血的刽子手。你可以去，你看的时候是什么态度呢？这什么玩意，这是假的。可以参观，靖国神社我也去过，可以参观不能参拜，你要参拜丧心病狂的法西斯极右翼分子，你就完蛋了。<br />
<br />
&nbsp; &nbsp; 15.天安门广场我是从来不去，我开车从那里经过，我都鸡皮疙瘩，四千多万冤魂索命啊，太可怕了，那个地方就应该改成大屠杀纪念馆，纪念碑也是，把那碑文铲了，改为：死于1949到1978政治恐怖的人们永垂不朽，应该有这个殉难者纪念碑。<br />
<br />
&nbsp; &nbsp; 16.几千万活活饿死，你还说他是伟大领袖？他是伟大的领袖，这就太可怕了，你这个民族就是猪一样的民族，一点都不懂得反思。你在德国能说希特勒是伟大的领袖吗？<br />
<br />
&nbsp; &nbsp; 17.有说毛是为了中国好，他是为了自己的权力好，他为什么要造原子弹？他不是为了要保卫中国，他是为了保卫他屁股底下那把椅子，他不被别人推翻，跟今天北朝鲜干的事一样。<br />
<br />
&nbsp; &nbsp; 18.这在中华五千年文明史上，甚至在人类一万八千年文明史上都是最黑暗的十年，能跟它勉强媲美的是，斯大林的俄国和希特勒的德国。<br />
<br />
&nbsp; &nbsp; 19.毛有个特点，他比较热爱女青年，皇上都这样。&nbsp;&nbsp;<br />
&nbsp; &nbsp; 20.毛作为一个人，后面加一个字：渣，他是个人渣，战友情、夫妻情、父子情什么都没有，眼里只有一种东西，什么？权力。<br />
<br />
&nbsp; &nbsp; 21.二十世纪三大魔王，希特勒好歹杀的是外国人，德国人应该说他杀的不太多，斯大林和毛杀的都是本国人，斯大林匪帮残害本国公民，但他是通过法律手段，靠警察杀人，毛是靠谁杀人？谁想杀人就可以杀人，人人都有杀人权，你这样看起来，二十世纪最黑暗的一幕真的就发生在我们生活的这块国土上，而且并不遥远。<br />
<br />
&nbsp; &nbsp; 22.（毛毁灭文化）你毁灭中国文化，你就是汉奸，谁这么干谁就是汉奸。你把中国文化彻底毁灭，你就是最大的汉奸。<br />
<br />
&nbsp; &nbsp; 23.无耻文人们讲，毛熟读史书，这我们不否认，毛只看史书，看了一肚子的阴谋诡计，问题是历史上有益的经验他没有一条吸取的。<br />
<br />
&nbsp; &nbsp; 24.（林彪913）毛第六感官惊人的发达，我玩人玩了一辈子，你想玩我？你小样。<br />
<br />
&nbsp; &nbsp; 25.谁要说那会儿没有腐败，谁简直不是人，那会儿的腐败比现在厉害得多，那会儿的腐败是制度性腐败，就跟中国古代似的，是制度性腐败。它是出身的不平等，现在是财产的不平等，财产可以通过个人奋斗来获得，出身能吗？<br />
<br />
&nbsp; &nbsp; 26.毛死了，1949以后，毛做得最正确的一件事就是毛自己死掉了。<br />
<br />
&nbsp; &nbsp; 27.把毛的身体掏空，填上一些乱七八糟的东西，一缝，啪，往天安门一摆，暴尸，所以有了一个毛的停尸房。<br />
<br />
