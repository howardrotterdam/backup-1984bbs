&nbsp; &nbsp; 周恩来的背景和实力的来源大家都不陌生，就是情报特务工作，讲的直接点就是收集情报，暗杀，绑架，煽动暴乱，游行，掠夺等等，如果大家了解一些共产党政权的特点，你会发现共产党政权能够非常成功的控制国民的很重要的一点就是其遍布全国的庞大情报网，在共产党国家里，你最信任的人，包括你的家人，妻子，丈夫，儿女，朋友，同事，邻居等等，你最亲近的人就是出卖你的人，就是迫害你的罪魁祸首，因为意识形态导致人际关系和感情血缘亲情完全被政治意识取代，国家已经不是以家庭为单位而是以基层组织为单位，而所谓的组织就是遍布全国的情报网，在中国决定一切的核心机构是中国的军队，但是决定军队的力量有两个，一个是领袖的决策，另一个是渗透在军队的情报机关，所以中共的管理系统在毛泽东，周恩来在世时有两个，一个是毛泽东的党和行政管理体系，另一个就是周恩来的遍布全国各地的情报体系，两大体系都可以号令全国。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;如果大家都了解情报工作，相信都应该明白中共所有的领袖，党员，甚至全国所有的公民的个人资料，包括鲜为人知的隐私在周恩来面前都是完全公开的，甚至直接的讲在中国境内的一切居民的一切活动，言论等等都在周恩来的系统的监视之下。这一点并不随周恩来成为总理而发生改变，直到周恩来死亡之前，周恩来一直控制中国的一切情报系统，当然也包括毛泽东的个人资料。这一段话或许不太重要，但是在下面的关于文革的论述中你会发现它的威力。 (博讯 boxun.com)<br />
<br />
&nbsp; &nbsp; 一，周恩来的情报体系<br />
<br />
&nbsp; &nbsp; 可以说中共的很多最重要的系统都是周恩来创建的，包括情报特工系统，这些系统在国民政府统治时期对制造混乱，暴乱，暗杀，绑架等等具有很大的作用，有人常常将中共的成功夺权归结为军队的不抵抗导致的养精蓄锐，导致壮大，但是我本人坚持我的观点，就是中共的军队依然是不堪一击，不过是乌合之众，在当时的时候仅仅是由于苏联军队的武装干预才导致夺权成功，而中共仅仅是做了收尾工作而已，林彪的四野横行全国，毛泽东之所以依赖四野来武力占领全国，其原因就是四野的核心就是苏联兵团，就是苏联人打天下，毛泽东坐江山。抗日，中共屁都没放几个，而且放的都是闷屁。中共经常吹嘘在国民政府的统治区域和日占区域发动了所谓的第二战场，这个所谓的第二战场到是的确有，但是它的指挥中枢确是周恩来的体系。周恩来是通过暗杀起家的，提起暗杀的人相信大家随便翻翻历史资料就可以了解，能成为中共的领袖很重要的一点就是要没有人性，更要杀人成性，否则就会被别人杀掉，唯一保存生命的方式就是杀掉别人，这是共产党的生存法则，与生俱来。<br />
<br />
&nbsp; &nbsp; 而且在共产党国家有一个不变的真理:生存下来的没有受到致命迫害的很多人都是告密者。<br />
<br />
&nbsp; &nbsp; 我不想太讲述周恩来的工作经历，那是历史学家的工作，对该段的定义就是周恩来创建了中共的情报暗杀绑架体系。<br />
<br />
&nbsp; &nbsp; 二，建国前的评价<br />
<br />
&nbsp; &nbsp; 我对周恩来的在建国前的评价是一个特务在乱世偶然成为中共的领袖，但是很快被毛泽东赶下台，从1945年，中共，国民党，美国，苏联在中国大陆的四国演义中，周恩来一直精心于权力，与毛泽东一样，周恩来也痴迷权力，为获得权力和地位，周恩来可以放弃一切，周恩来了解控制中共，保存生命，拥有权力的方式，因为周恩来本人并不具有成为核心领袖的能力，他的专长是特务情报，而不是政治决策，就是说他是非政治管理型的人物，与汉武帝时期的张汤，武则天时期周兴、来俊臣、索元礼，明朝的东厂，西厂，锦衣卫，蒋介石时期的戴笠，毛人凤一样，中国有一句古语，了解古史可以洞查今日之政。但是古今中外的任何一个制度都没有共产党政权一样的残暴，如果说共产党是一个残暴的杀人如麻的匪徒组织起来的，而周恩来又置身其中，与共产党领袖为伍，并身居高位，屹力不倒，仅随毛泽东之后，唯一的解释就是他与毛泽东一样残暴凶狠，能在共产党队伍中经历磨难与屠杀而留下的的确都是如中共做称的都是精英，都是杀人如麻，视国人如蚂蚁的屠夫。虽然将周恩来叫做中国的贝利亚，但是又很不情愿，因为与周恩来相比较，贝利亚简直不值一提，因为，贝利亚杀的人连周恩来的零头都不到。<br />
<br />
