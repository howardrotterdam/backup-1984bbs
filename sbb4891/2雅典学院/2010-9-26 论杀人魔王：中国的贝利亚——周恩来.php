&nbsp; &nbsp; 可以说文革的一切屠杀，迫害，镇压都离不开周恩来的热情参与，是周恩来指挥了文革的大屠杀，是周恩来执行了大迫害，是周恩来下令武力镇压。我们经常听到中共的解释是周恩来是被迫执行毛泽东的意图，但是实际上周恩来是心甘情愿的而且是非常高兴的执行毛泽东的命令，完全是自愿，没有任何的被迫的意思，因为他是受益者，所以，毛泽东与周恩来联合发动的文化大革命使周恩来的情报体系空前强大，终于在周恩来的体系下，人间悲剧出现。<br />
<br />
&nbsp; &nbsp; 有人说周恩来挽救了大批的干部，因此，非常感激，的确这些数不清的共产党官员的确是周恩来拘捕下放的，也是周恩来救的，但是周恩来的行为并不是他个人的意思，而是执行毛泽东的意图，也就是说这些被免于屠杀迫害的共产党官员得以幸免完全是毛泽东手下留情，并不是周恩来仁慈，中共的宣传仅仅是转移政治矛盾，树立周恩来的光辉形象而已。需要了解，如果毛泽东决定杀人，任何人都无法阻止，连周恩来也不例外，邓小平，胡耀邦，万里，等等中共领袖的幸存完全是毛泽东的决定，如果毛泽东决定杀他们，周恩来是保不住的，就是因为毛泽东没有杀他们的意图，因此，才有周恩来保护广大干部的传闻。在这其中，哪些人要杀，怎样杀，哪些人不杀，怎么对待，周恩来的体系人员有严格的区分，而且态度完全不同，完全取决于毛泽东的决策。<br />
<br />
&nbsp; &nbsp; D，周恩来的不倒原因<br />
<br />
&nbsp; &nbsp; 在中共建国的1949年到周恩来死亡的1976年的27年中可以说周恩来的地位非常的稳定，如同叶剑英一样稳如泰山。实际上解释毛泽东的不倒原因很多，可以简单归结为几个原因。<br />
<br />
&nbsp; &nbsp; D1，周恩来的太监身份的影响<br />
<br />
&nbsp; &nbsp; 毛泽东身边的中共领袖除了周恩来之外基本上都地位不稳，为什么毛泽东如此信任周恩来呢?相信大家对于封建社会的太监并不陌生，太监由于没有生殖能力，无法繁衍后代，加上太监可以在皇帝的特殊授权下获得特权，完全效忠皇帝。因此很多皇帝将太监作为心腹，执行特殊的行动，实际上周恩来也具有这个特点，由于自身的原因，周恩来没有生育能力，但是有淫乐的能力，所以无法繁育后代，而且周恩来的权力来自毛泽东的授权，在毛泽东眼里，周恩来不过是个听话的太监而已，历史上的故事已经告诉了毛泽东，如何利用周恩来。可用但是不可授予名份，因此，周恩来一直就是总理，而非接班人，而没有成为接班人也是表明没有政治野心的意图，毛泽东需要周恩来的目的就是看家护院，所以可以了解的很直接。<br />
