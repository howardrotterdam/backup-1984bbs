&nbsp; &nbsp; 周恩来很重要的一个特点就是他的出色的特务管理能力，可以迅速完成毛泽东的命令，而且毫无人性，无论是亲属，同事，朋友只要是毛泽东的命令都可以执行，在命令的执行方面。周恩来是毫不迟疑，只要是毛泽东的命令就是正确的，只要是毛泽东反对的就是错的，就是要反对的，就是敌对势力。这些特点使其深受毛泽东的赏识因而被委以重任。<br />
<br />
&nbsp; &nbsp; 六，毛周地位的稳定及外交影响<br />
<br />
&nbsp; &nbsp; 文革的空前迫害和镇压使毛泽东的命令成为国家的一切的核心，而周恩来就是毛泽东命令的发布执行方，毛泽东的一条条命令发向周恩来，再由周恩来发向全国。经过文革的过程，中国建立了以毛泽东为绝对核心，周恩来辅助，四人帮具体操作的管理体系，在这个体系中，我们可以很清楚的了解周恩来的地位和权力，作为毛泽东的忠实仆人，周恩来获得了绝对的稳定的地位，只到死亡。毛泽东和周恩来的地位稳定是在毛泽东疯狂的迫害镇压决策下，周恩来的严密执行中实现的，周恩来的迫害镇压执行使中共的元老死伤怠尽，可能的反对力量的消灭使毛周的地位空前稳定，舆论的神话使其获得了绝对的地位，在毛泽东进行对外扩张，输出革命的时候，周恩来也是具体的执行首脑，对中国周边国家的香港，东南亚，日本，韩国，非洲等国进行革命输出的执行人也是周恩来，毛泽东的一切决策的参与，具体的执行人员中，周恩来都是不可缺的核心人物。<br />
<br />
&nbsp; &nbsp; 七，抛骨灰与盖棺定论<br />
<br />
&nbsp; &nbsp; 在中共的领袖中，周恩来是要求不留墓地的，因为，他杀的人太多，未来的结局可以预料，必然是开棺鞭尸，无论是百姓还是共产党官员都对周恩来咬牙切齿，如同很多被纳粹屠杀的人虽然痛恨希特勒但是更加痛恨下层的具体执行人一样，周恩来给很多的中国人带来无尽的伤痛与悲哀，无论中共如何粉饰周恩来都无法掩盖他与毛泽东合谋屠杀迫害中国百姓的事实，无论未来如何，作为臭名昭著的中共杀人不眨眼的混世魔王，周恩来作为邪恶的代表将永远被人们吐弃。<br />
