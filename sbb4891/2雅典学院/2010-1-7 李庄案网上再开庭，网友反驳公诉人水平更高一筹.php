													<h2>李庄案网上再开庭，网友反驳公诉人水平更高一筹</h2>
						
						
																											<div id="postmessage_418867" class="t_msgfont">（参与网2010年1月7日讯）：12月30日9点10分，重庆市江北区法院开庭审理了由区检察院起诉的北京律师李庄涉嫌辩护人伪造证据、妨害作证罪案。重庆检察机关派出了优秀公诉人、代理检察员贺贝贝、么宁出庭支持公诉。北京著名律师高子程和陈有西担任被告人李庄的辩护人。双方唇枪舌剑，辩论极其精彩。因该案社会影响大，吸引了全国各地数十家媒体记者，以及人大代表、政协委员和法律界人士旁听。　 (博讯 boxun.com)<br />
<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;日前，法院尚没有做出判决，公诉人在网络上公布了自己的控诉观点，有网友与之激辩，水平更胜一筹。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;据悉，公诉人与辩护人最精彩交锋的集中在李庄妨害作证的行为是否符合法律规定的犯罪构成特征上。辩护人认为，李庄表示，对公诉机关提供的108名证人，李庄并未与其接触，更谈不上妨害作证。李庄及其辩护人还多次提到，本案中公诉人出示的大量证据只有口供，并无伪造证据的物质载体。 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;对此，公诉人反驳说，刑法第306条的“妨害作证”是指“引诱证人违背事实改变证言或者作伪证”，引诱既包括利益引诱，也包括非利益引诱。至于这种引诱是要直接接触证人来实施，还是通过第三人的行为来实施，均不影响引诱行为本身的认定。因此，李庄虽然没有见过控方提供的108个证人，但是否见面不是认定“引诱”行为成立与否的要件，被告人提出的这一观点不影响指控成立。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;公诉人还认为，被告人的供述属于刑诉法规定的七种证据之一，李庄实施的行为是属于帮助龚刚模伪造供述的行为。伪造证据是指制作并不真实的证据的行为，从根本上说伪造证据是一种故意捏造事实的行为，捏造的事实通过证据这一载体反映在刑事诉讼中，伪造证据既可以表现为编造一个原不存在的新的证据，也可以表现为对已经存在的证据进行篡改、歪曲，对其证据资格和证明力通过行为人违法的行为来改变，也可以视为伪造证据。 <br />
&nbsp; &nbsp; 　　公诉人的结论是，辩护人伪造证据、妨害作证罪系行为犯，李庄的犯罪行为已经完成，龚刚模是否实际翻供、程琪是否作伪证，不影响对李庄犯罪事实的认定。因此不应以是否有物质的证据来衡量李庄的行为是否完成。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;天涯社区网友ZB666031认为：2010年1月5日龚刚模在法庭上说，没有受到刑讯逼供，其先前举报李庄也是真的。这样，李庄会不会因为在取证过程中，希望找到医生作证而构成伪证罪呢。 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　《刑法》306条原文是： <br />
&nbsp; &nbsp; 　　 在刑事诉讼中，辩护人毁灭、伪造证据，帮助当事人毁灭、伪造证据，威胁、利诱证人违背事实改变证言或者作伪证的，处三年以下徒刑。 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　 辩护人提供、出示、引用的证人证言或者其他证据失实，不是有意伪造的，不属于伪造证据。 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　 从以上条款可以看出：威胁、利诱证人违背事实改变证言或者作伪证的，处三年以下徒刑。李庄在龚刚模先说了被刑讯逼供后，经进一步了解确信其说后（从李庄到中院反映情况就可以证明李是相信龚刚模说的被刑讯逼供的事实），所以李庄收集相关证据是履行称职律师的职责，只要李庄没有要求医生作假证，李庄就不构成伪证罪，为什么，因为，如果李庄见了医生，李庄告诉医生说，龚刚模说被刑讯逼供，是不是真的，（这一点，李庄不构成犯罪），之后的情况有可能是： <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　（1） 医生回答说，不是真的，龚刚模没有被刑讯逼供，如果李庄在这时要求、威胁、利诱医生违背事实证明龚刚模被刑讯逼供了，并付诸实施，李庄就构成了伪证罪。如果医生没有付诸实施，不构成伪证罪，因为306条中说“威胁、利诱证人违背事实改变证言或者作伪证的，处三年以下徒刑。”306条中没有说“威胁、利诱证人违背事实改变证言，作伪证或者准备作伪证的，处三年以下徒刑。”（注释：法律条款不能随便推理，严格遵守法律条款是公平公正的基础之一，如果不能做到这一点，就有可能乱套）。 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　（2）　医生回答说，是真的，龚刚模确实受到了刑讯逼供，如果李庄要求医生证明龚刚模受到了刑讯逼供，医生作证与否，李庄不构成伪证罪。 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　（3）　龚刚模实际是否被刑讯逼供，与李庄是否构成伪证罪没有必然关系，请看306条中：辩护人提供、出示、引用的证人证言或者其他证据失实，不是有意伪造的，不属于伪造证据。这里，李庄收集证据，包括人证，即或失实，是否在法庭发生，只要李庄不是故意，仍然不构成伪证罪。 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　李庄案件中，龚刚模向李庄说被刑讯逼供，李庄进一步了解后也相信龚刚模被刑讯逼供了，这时李庄收集相关证据如果失实，只要不是故意的，就是龚刚模没有被刑讯逼供，李庄也不构成伪证罪。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 这位网友还说： 按照法条，“辩护人毁灭、伪造证据”和“辩护人帮助当事人毁灭、伪造证据”在这里并列，因此，这里伪造的证据必须有标的物或者形，即辩护人自己毁灭、伪造证据，属于伪证罪，辩护人帮助当事人毁灭、伪造证据属于伪证罪，这里的证据不能包括语言或者思维，为什么，因为如果包括语言或者思维，辩护人自己改变了自己的语言或者思维，与改变当事人的语言或者思维属于同罪，而辩护人的语言或者思维有可能随时改变，这也是犯罪？ <br />
