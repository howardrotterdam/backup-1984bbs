																											<div id="postmessage_282413" class="t_msgfont">孙文的一生以二次革命为界。前半生为民族主义和民主主义的革命摩顶放踵，推动了两千年帝制的瓦解。这一点是功在千秋的。但后半生他为了追求单纯的民主主义目标，却开始背离早年崇尚的英美式的宪政民主道路，过分推崇武力和领袖的个人独裁，先后与他的四个亲密战友决裂，也与四种宪政转型的道路决裂。其一是与反对一党专制的章太炎决裂，其二与主张议会主义道路的宋教仁决裂，其三与主张法治主义道路的黄兴决裂，最后与主张联邦主义道路的陈炯明决裂。当这四种方向被放弃之后，最终孙文只能开创出“一个国家、一个政党、一个主义”这一中国史上前所未有的现代意识形态专制模式，以及国民党长达80余年的党国传统。对蒋介石建立在训政名义下的独裁政治负有不可推卸的责任。尤其是为共产党在大陆建立有史以来最严酷和彻底的极权主义政体，扫清了大部分路障。所谓成也萧何，败也萧何，中国这一百年追求宪政与共和的传统发生断裂，走入困境。很多因素是从孙文那里就开始了的。我并非要求孙文本人对这些因素负有严格的责任。孙文的悲剧是整个中国一百年宪政史的悲剧。但看不到这些误区和悲剧因素，甚至还在不断的帮助两岸的统治者对孙文进行神化。却是中国之所以仍然陷在极权主义政治中不能自拔的原因之一。</div>

							
							
							
							
