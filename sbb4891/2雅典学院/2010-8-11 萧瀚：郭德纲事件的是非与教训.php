郭德纲事件的是非与教训<br />
<br />
 <br />
<br />
萧瀚<br />
<br />
 <br />
<br />
近十日来，除了南方报系外，大江南北的媒体（尤其是部分京城媒体）机器几乎全面启动，对付一个艺人的错误言行，郭德纲事件迄今的结果似乎遂了某些人的愿。但此次事件反映出不少问题，涉及各方面，颇值细究。<br />
<br />
 <br />
<br />
事件缘起：7月底，BTV《每日文娱播报》曝出郭德纲涉嫌私占小区公用绿地，8月1日，BTV两名记者（此次事件中的另一位主角周广甫记者及其女同事）去地处大兴县的郭德纲家别墅采访。<br />
<br />
 <br />
<br />
一．BTV记者周广甫等是否涉嫌私闯民宅<br />
<br />
 <br />
<br />
住宅是人最后的心理安全堡垒，不尊重住宅是蔑视人权最严重的思维方式之一。因此，是否私闯民宅是此事件中很重要的问题。如果今天允许记者随便骚扰郭德纲这样不掌握公权力的名人，明天就别怪包括记者、警察在内的任何人不把自己当外人。有人担心讨论这问题会危及新闻界针对涉公共事务官员的信息采集，但是，第一，掌握和不掌握公权力在享有隐私权方面差别很大，不可划一；第二，即使是对公共官员，也没有任何一国法律剥夺他们所有隐私权，他们也享有基本人权，其公民权只有在涉及公共利益的内容受限。不少人讨论公共事件时，不管涉事者是涉公共利益的官员还是个人，常常把应该区别对待的两种人的公民权混为一谈，导致动辄拿未经论证的“公共利益”（甚至可能根本不是公共利益）强奸公民权――他们很容易忘了也许下一个被强奸的就是他们自己。<br />
<br />
 <br />
<br />
当媒体对郭德纲铺天盖地一片谴责之声时，网上论坛（尤其是微博客）上却争论激烈，并不是那么一边倒地反对郭德纲，究其原因，就与周广甫及其女同事是否私闯郭宅有关。<br />
<br />
 <br />
<br />
从网上流传的那个完整版偷录资料看（资料来自德云社的网站，目前已被黑），详细情况是：<br />
<br />
 <br />
<br />
女记者走上楼梯，敲门。保安：这儿小区不让，不让这样。周广甫：谁说的，你说的呀？保安：不是我说的，是我们物业说的。周广甫：啥时候说的？前天我们还来过这儿。女记者：不拍照。<br />
<br />
 <br />
<br />
可见，周广甫及其女同事从一开始就规避了小区不允许陌生人进入的要求，并且不顾保安的阻止而去敲门。李鹤彪出来，双方交谈几句后，周广甫质疑物业给郭家写的证明是啥意思，李鹤彪因此发怒，遂动手打周广甫。可见，最初的私闯民宅是成立的，但后面发生的事情改变了这一性质（参见下面第三点）。<br />
<br />
 <br />
<br />
二．BTV记者周广甫偷录的性质<br />
<br />
 <br />
<br />
周广甫及其女同事涉嫌私闯民宅几无疑问，挑衅式、钓鱼式“采访”找打，如此敌意的采访姿态，只能有一个解释，仗着偷录罢了。通常情形下，未经当事人许可的音像录制是侵犯他人隐私权的行为，但并非在所有情形下都是违法的，往往与所关涉的事情性质相关。倘若事关公共利益，公开录制有困难时，偷录当事人与公共利益相关内容属于合理范围。但此事件中，郭德纲是否圈占小区公用绿地，属于私产纠纷范畴，不涉及公共利益，郭德纲本人也没有什么特别的人身安全威胁性，偷录没有理由。除非周广甫估计自己一定会挨揍，不然偷开摄像机干什么？用如此挑衅的语言对待被采访人干什么？<br />
<br />
 <br />
<br />
另外，有人认为狗仔队偷录是正当的，因为全世界都这么做。全世界都这么做并不是论证一个道理的逻辑理由。狗仔队之所以存在，是因为他们侵犯的不是公共利益，而是明星们的私人利益，因此，明星们有权决定是否放弃其部分利益。明星们通常是既厌恶狗仔队又离不开狗仔队，没有他们，明星们的信息没人发布，难有名利市场，容忍他们，明星们的隐私就可能遭到侵犯。为此，世界各国常有明星们和狗仔队之间的纠纷，动辄恶言相向，甚至暴力冲突都常见。一句话，狗仔队的存在不是因为他们有这权利，而是明星们放弃了自己的部分权利。狗仔队们刺探明星们的隐私是否合法取决于被刺探明星们的意愿，而不取决于其他。<br />
<br />
 <br />
<br />
为此，BTV偷录一事没有正当理由，涉嫌侵犯隐私权。郭德纲可以通过诉讼解决此问题。<br />
<br />
 <br />
<br />
三．李鹤彪打人的性质<br />
<br />
 <br />
<br />
如果李鹤彪出门来，拒绝采访，并且赶两位记者走，他们不走，李鹤彪打人，那还可能构成正当防卫。但由于李鹤彪开门，与女记者交谈，并且回屋拿证明书，这相当于默认接受采访，且相当于对两位记者涉嫌非法私闯民宅之举转为合法的事后追认行为。<br />
<br />
 <br />
<br />
视频显示：女记者敲开郭家门之后，李鹤彪站门口，双方交谈了几句，李鹤彪回屋拿出一张文书，大意可能是承认开发商允许郭德纲圈绿地，周问：这谁写的？女记者：物业写的。周：物业写这啥意思？李鹤彪很不客气回了一句：你说啥意思？一边说一边打周广甫。<br />
<br />
 <br />
<br />
可见，打人是在李鹤彪接受采访过程中发生的，李鹤彪违反了《治安处罚法》，殴打他人，现已被拘留并罚款，已经承担了法律责任。其实周广甫非正常的挑衅性采访是激怒李鹤彪的一个重要原因――周广甫甚至说：“我就私闯民宅怎么了？”――当然这种话可以视为特定语境下的气话，但这种气话难道不是特别拱火找打的话吗？李鹤彪事后否认自己打人，敢做不敢当，可说很无赖，如果不是这偷录，还真难让他承认。<br />
<br />
 <br />
<br />
四．郭德纲这张嘴<br />
<br />
 <br />
<br />
打人事件发生后的当晚，郭德纲将打人事件编进相声段子：“今儿我徒弟打人了，打就打了呗。过两天给他搞一专场，智斗歹徒民族英雄专场。”这种话即使只是调侃，也过分了，但媒体将其过度渲染显然也有点不顾语境的夸张，毕竟这是在说相声的时候说的。<br />
<br />
 <br />
<br />
另外，他还说“关于北京电视台，我也是很无奈，这么些年，说大话使小钱，很龌龊的一个单位。”从此次BTV的做派看，我倒觉得没说错。<br />
<br />
 <br />
<br />
“记者啊，还不如妓女。我一直在想，这妓女在红灯区活动，记者呢在绿灯区，想去哪儿就去哪儿。”这段话，应该看语境，而不应该认为他是在骂每一个记者，郭德纲在其8月3日的媒体说明会上做了解释，这辩解并不强词夺理――他有权如此看待周广甫等记者。<br />
<br />
 <br />
<br />
“当初开发商许给我，这里是我的后院，开发商一走，我们院里有几个穷人，成立一业主委员会。天下之大无奇不有，就有这浪催的人。”这些话显然都是触犯众怒的，但那种语境下，主要还是他对业委会的反感情绪，倒不是专门针对穷人的，不过无论如何都会被看成得志猖狂――这是无可避免的。<br />
<br />
 <br />
<br />
对记者以及BTV的挖苦嘲讽，尤其反映在他8月4日的博文《有药也不给你吃》一文中，大致意思与上述同。博文中，郭德纲回应了“圈占绿地”问题：“当初买房时，开发商将小院用木桩围起，我未扩寸地，仅将旧桩换做新桩而已。有人不信，请找来开发商理论，我随时恭候！”但媒体至今尚未对此问题核实。<br />
<br />
 <br />
<br />
许多人对郭德纲这篇博文极为反感，认为郭德纲流氓气十足。但是，如果从郭德纲角度想一想，BTV利用自己的话语权，剪辑偷录的视频材料，周广甫借挨打博同情，满嘴跑火车撒谎，误导、发动整个舆论对付自己，他如何咽得下这口气，又凭什么咽下这口气？他强项支持李鹤彪打人固然不对，但也并非强词夺理――而是他难以理解过于复杂的法律问题。要比流氓的话，这次还有谁比BTV更流氓？人家BTV为什么可以随便私闯民宅、随便偷录不必道歉，我家人打了人就一定得道歉？这种心理很符合人的基本公平需求，我相信许多人都会在这种情况下不肯认自己的错。在强势者与弱势者之间发生冲突时，如果双方各有错，强势者不肯认错，弱势者但凡有点尊严都不愿低头，而如果弱势者主动先认错，强势者就更不会认错了。<br />
<br />
 <br />
<br />
五．BTV的媒体发布会<br />
<br />
 <br />
<br />
8月2日，周广甫在BTV的发布会上神思凝重地撒了一通谎，什么摄像机被打坏了，人被摁倒了，从楼梯上滚下来之类，通通子虚乌有，郭德纲后来以监控录像澄清此事。BTV将摄录资料进行剪辑，删去两位记者私扰郭宅的内容。说明会和新闻播报，BTV误导观众和媒体、构陷郭德纲之举至此全部完成。<br />
<br />
 <br />
<br />
BTV的这种行为完全没有职业操守。<br />
<br />
 <br />
<br />
六．媒体齐轰郭德纲及其音像制品下架、德云社自查<br />
<br />
 <br />
<br />
8月2日开始一直到现在，全国（主要是京城）多家媒体齐轰郭德纲，尤其是4日和5日最多，简单罗列，就有“郭德纲不能太缺德了”（北京晚报）、“对待无良演员封杀也许是最好的办法”（新华网）、“郭帮主，您还是收了神通吧”（新京报）、“谁来给郭德纲补补课”（京华时报）、“郭德纲上演硬暴力与软暴力”（新京报）、“郭德纲，拿道德玩幽默的玩笑你开不起”（华商晨报4日）、“郭德纲别拿穷人当对立面”（东方早报）、“郭德纲与流氓文化”（新闻晨报）、“法治社会岂容郭德纲如此纵横”（新华每日电讯）、“郭德纲不跟中国人讲理，想跟哪国人讲理啊”（环球网）、“郭德纲被圈了”（搜狐娱乐）。甚至连CCTV都不指名地用排比句痛批郭德纲是三俗代表，而人民日报、新华社也都发表批评郭德纲的评论。与此同时，相声界元老姜昆也出面声称郭德纲是他们2007年以来一直要反对的“三俗”。<br />
<br />
 <br />
<br />
接下来的事态急转直下：8月5日，李鹤彪被北京市经济开发区公安局行政拘留7天，罚款200元。同日，郭德纲接受专访，称弟子打人完全合理；8月6日，郭德纲弟子何云伟、师弟李菁宣布即日起退出德云社；8月7日凌晨，德云社在官方网站上发表“停演整顿”的声明，郭德纲的书、音像制品在各大官营书店下架，记协发表谈话，表示要维护记者合法采访权；8月9日凌晨，德云社网站被黑。<br />
<br />
 <br />
<br />
至此，德云社遭遇其成立以来最严重的危机。<br />
<br />
 <br />
<br />
媒体如此一致地对一个相声演员发难，可谓极其罕见，个中原委难以猜测――到底是十数家媒体因为被BTV蒙蔽而对郭德纲不够公正地发难，还是另有串联甚至有公权力授意，这些都暂不可考。<br />
<br />
 <br />
<br />
终于，8月7日――这是德云社“自查”、郭德纲音像制品下架后，《经济观察报》发了第一篇质疑媒体在郭德纲事件中是否公正的评论：“在郭德纲事件中媒体有失公正”，随后，《南方都市报》、《南方报网》等数家媒体也质疑BTV及其它媒体对待郭德纲是否公正以及质疑公权力封杀郭德纲的合法性问题，同时，五岳散人、王小山、李承鹏等各位也有评论直接抨击公权力的胡作非为，冯小刚、赵丽华、郝蕾等艺人也都站出来支持郭德纲，抗议公权力的介入。<br />
<br />
 <br />
<br />
七．采访权与记者证问题<br />
<br />
 <br />
<br />
郭德纲在8月3日媒体说明会上质疑周广甫及其女同事有没有记者证，在8月1日周广甫被打时，周广甫认为采访权是国家赋予的权力。这两种观点都有问题，采访权发端于公民的言论自由、社会的新闻自由，而不是来自国家赋予，国家不赋予，人也有这权利，记者所拥有的采访权无非是在某些很特殊的情况下比普通人理应得到更多保护而已。因此，不必有政府垄断核发的记者证，此证之存在，唯一的原因和结果就是中国没有言论自由、新闻自由。郭德纲作为一位相声演员，不懂这个道理很可理解，而周广甫不懂则难以理解，因此不可不辨。采访权通常需要被采访对象的自愿配合才能实现，被采访对象拒绝，则其无以实现。<br />
<br />
 <br />
<br />
八．公共人物问题<br />
<br />
 <br />
<br />
媒体在批评郭德纲支持徒弟打人一事中，通常会提到郭德纲是一位公共人物（或称公众人物），应该以较为得体的公共形象面对大众，而公然支持徒弟打人，未尽公共人物的义务，败坏社会风尚；还有人认为，郭德纲有义务拿出购房协议、产权证以证明圈占绿地的正当性。<br />
<br />
 <br />
<br />
郭德纲当然属于公共人物，因此若单究李鹤彪打人一事，前一类谴责大致成立，但不完全公正。每一件事情都不是孤立的，李鹤彪打人一事尤其不是孤立的，李鹤彪打周广甫之前，曾说到BTV在此前的电视报道中，居然拍错了房子，将别人家当成郭德纲家（但BTV至今没有核实和回应此事）――可以说郭家本来就窝了一肚子火。而周广甫如此敌意的挑衅性“采访”简直就是“找打”――但由于李鹤彪默认接受采访在前，所以只要动手打人，李鹤彪就错了。而其后，BTV与郭德纲经纪人王海之间交流不畅，未经证实的BTV要“五万元”封口费以及威胁若不给便扩大事态，可能是导致郭德纲写《有药也不给你吃》痛扁BTV的重要原因。<br />
<br />
 <br />
<br />
郭德纲之错，错在将此事的前后是非曲直一锅烩，拒不认错，跟许多一屁股坐在BTV腿上的意见性质类似，但并没有BTV那么坏。郭德纲正确的做法应该是要么保持沉默，若要出来说话就得批评李鹤彪打人一事，其他的是非曲直另外评说。<br />
<br />
 <br />
<br />
至于有人认为郭德纲有义务拿出购房协议、产权证以自证其错（对）的想法，则有待商榷。公共人物并非对一切被关注的事情都有义务向公众交代，关键看涉及的事情是否涉及公共利益。郭德纲涉嫌圈占小区共用绿地，这是典型的民事相邻关系，属于私产纠纷，根本不属于公共利益，人们对此事没有不可剥夺的知情权，此事本应该由发生纠纷的当事人之间自行解决，无法解决时按照民事司法程序解决，而不是由大众来舆论审判。因此，郭德纲没有义务拿出自己的房产证和购房协议，倒是质疑他圈占绿地者有义务举出足够证据来证明自己的怀疑。<br />
<br />
 <br />
<br />
演艺界的明星们虽然是公共人物，但他们与公权力领域以及教育界、医疗界、宗教界、慈善界、司法界等圣职领域的公共人物不同。古今中外的历史都告诉人们，演艺界或艺术界人士，往往是人类中最特立独行的人群，这种特立独行也往往表现为对许多传统伦理规范、生活规范的冒犯，因此，上述各界人士如果被要求成为道德楷模，但演艺界明星们从来就不是人类的“道德”楷模。各国影星、歌星的私生活方式与大众有较大差异，但人们往往容忍他们而不会容忍上述六大圣职领域的公共人物过于脱离普通规范。<br />
<br />
 <br />
<br />
对于演艺界明星们，不必用道德楷模去要求他们，只要求他们守住人之为人的道德底线就行了。这次的事之所以有该批评郭德纲之处，是因为他支持李鹤彪打人。他支持李鹤彪打人有他的理由“偷拍不挨打，此事古难全！”虽然这与事实不符，前文已说李鹤彪默认接受采访使得周广甫等二人私闯民宅被追认为合法，李鹤彪打人时也不知道周广甫在偷录，但确实很难要求一个以说相声为业的人那么清晰地厘清稍有点复杂的法律问题，试问那些对郭德纲进行最苛刻道德酷评的评论家们又有几个分析清楚这个问题了？因此，郭德纲支持李鹤彪打人的言论并没有有些人所想象或所歪曲的那样邪恶。<br />
<br />
 <br />
<br />
九．媒体在此事件中的教训<br />
<br />
 <br />
<br />
此次事件中，问题最严重的不是郭德纲、李鹤彪师徒，而是媒体以及后来封杀郭德纲的公权力。媒体存在下述几个问题。<br />
<br />
 <br />
<br />
第一，BTV记者周广甫及其女同事涉嫌私闯民宅，虽被李鹤彪默认接受采访追认为合法采集信息，但也为后来李鹤彪动手打人埋下祸根；<br />
<br />
 <br />
<br />
第二，周广甫及其女同事不合理使用摄像机，未经被采访人同意而偷录其言行，而并非在挨打之后打开摄像机，已构成涉嫌侵犯隐私权；<br />
<br />
 <br />
<br />
第三，周广甫在BTV的说明会上谎话连篇，除了李鹤彪打他这一个总体情节相符外，其他许多细节与完整的录像反映的情况相去甚远；所谓摄像机被打坏、他抱着摄像机滚下楼梯、检查摄像机时候才开始拍的、左臂脱臼（视频中有个镜头他伸出左手搭在一位旁观者肩膀上，如果左臂脱臼，还可能吗？）等都是子虚乌有之事；<br />
<br />
 <br />
<br />
第四，BTV既当原告，又当法官，断章取义，以有利于自己的方式剪辑过的偷拍资料误导其他媒体，误导大众，在本台节目中大骂郭德纲师徒；<br />
<br />
 <br />
<br />
第五，参与批评郭德纲的媒体，在基本事实不清的情况下贸然出手，不谨慎，尤其遗忘了BTV不可以既当原告，又当法官，这些媒体缺乏最起码的职业精神和职业操守；在郭德纲是否确实圈占小区公用绿地一事尚未明确之际，即认定其圈占为实，这种评论水准显然是极不专业的；同时，参与批评郭德纲的媒体，只有一两家媒体质疑过记者是否私闯民宅问题（但也由于受BTV剪辑过的偏私音像误导，基本上不可能准确分析，这可以不怪他们），但几乎不见质疑偷录问题的（这也是因为偏于BTV而没有对周广甫显然自相矛盾的谎言进行基本辨析的结果，他说摄像机摔坏了，又说检查以后拍摄了，撒谎也没撒圆）；<br />
<br />
 <br />
<br />
第六，批评郭德纲的媒体从国家级的央视、人民日报、新华社，小到普通地方报纸，对一个有错误言论的艺人用了整整一周以上的时间狂轰滥炸，用了各种各样道德谴责、人身攻击的言辞，却对BTV是否践踏新闻伦理、滥用话语权不置一词；<br />
<br />
 <br />
<br />
第七，当事态逐渐明朗，BTV造假、周广甫撒谎已经越来越明显之后，参与批评郭德纲的媒体理应意识到自己上当受骗，却为了维护自己的面子以及媒界江湖的潜规则而归于沉默，绝不为自己的不公正有半点歉意，甚至许多媒体持续跟进，依然对郭德纲落井下石。<br />
<br />
 <br />
<br />
毫无疑问，一味批评郭德纲、没有公正对待郭德纲、李鹤彪的BTV及其他媒体，应该向郭德纲、李鹤彪、公众严肃道歉。<br />
<br />
 <br />
<br />
十．不明部门涉嫌滥用公权力<br />
<br />
 <br />
<br />
这是这次事件中最严重的问题。一个相声演员的作品是不是“三俗”，公权力无权定性，大众自有评价，自有选择。一个开放社会，是一个人们可以在不损害他人的前提下，自由选择的社会，人们既可以选择高雅的精神产品或生活方式，也可以选择所谓“低俗”的精神产品、生活方式，不劳公权力决定。甚至可以说，不允许人们无害于他人的堕落，这样的社会，必是堕落的社会。郭德纲发表言论支持徒弟打人，若从前因后果看并没有错得那么离谱，更不意味着他的作品无权在书店里出售，任何权力部门都无权未经任何正当程序即勒令其作品从书店下架。迄今，各大书店里将郭德纲音像制品同时下架没有官方说法，这种无文书程序是公权力被滥用的极端标志，它剥夺了人们权利被侵犯之后基本的救济途径，是一种对权利的活埋行为，也是公权力最猥琐最可耻的行为方式。<br />
<br />
 <br />
<br />
除了侵犯人权，公权力介入此原本属于民间自由讨论的事件，还破坏了原本可能正常的公共议论，导致原本可能正常的批评都得兼以道义上首先表明捍卫郭德纲基本权利的前提，不然，本来可能正确的批评都会变成支持公权力滥用的落井下石。<br />
<br />
 <br />
<br />
任何一个正常社会，公民精神的基本特征都包含“警惕公权力，善待公民”。对公权力的批评几乎没有限制，因为即使批错，其自我修复与澄清功能极其强大，无需担心被冤枉而导致实质性损害，因此需要“恶意推定公权力”；而对公民却要善意推定，应该尽可能谨慎，一旦批错，就会给弱小的个体公民造成很大伤害。更何况在中国这种缺乏言论自由的环境里，一方面媒体缺乏自由，另一方面缺乏自律，尤其一些半公权性质的媒体往往还滥用话语权，一旦其话语权滥施之普通公民，就更难以救济。<br />
<br />
 <br />
<br />
另外，之所以要“恶意推定权力，善意推定公民”还有一个原因是，在事关公共问题以及人权等问题上，公权力理应比普通公民更懂，人们有权假设公权力的一切作恶都是明知故犯，但对公民却不能如此要求，正如前文已说到，如果评论家们都没能分析清楚周广甫等二人是否私闯民宅，为什么去要求一个相声演员如此懂行？<br />
<br />
 <br />
<br />
为此，批评普通公民，最好在讲清道理基础上留有余地，这也是媒体职业操守的一部分。<br />
<br />
 <br />
<br />
 <br />
<br />
但愿此次事件，除了促成郭德纲、李鹤彪、周广甫及其女同事的职业与公民意识自省，还能敦促BTV等媒体加强自律，至于公权力，不必寄望他们自我限权――唯有制度性的限权与分立以及真正的公民精神才能制衡们。<br />
