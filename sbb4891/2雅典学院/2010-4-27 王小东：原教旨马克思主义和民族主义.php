　　 但是对于这种现象，我们不能用抵制全球化来解决，我觉得这个是不可能的，也没有意义的。因为抵制全球化，也许对于这些国家的左派来讲是有意义的：至少资本不能外流，劳动者的谈判实力就要大一点。资本惹不起，都跑掉了，你谈判什么？你当然就完蛋了。可是对于中国来讲正好相反，中国如果劳动过剩资本稀缺，缺少跟劳动者谈判的资本就更糟了。本来中国劳动的谈判实力被压得非常可怜，如果资本比较多，劳动相对过剩的程度可以减少一点，劳动者的谈判实力不至于那么低。因此，资本流入中国可以增加中国劳动者的谈判实力，但是会妨碍其他国家的劳动者得谈判实力，所以那些国家反对全球化是有道理得，但是我们国家的左派反对全球化就是莫名其妙。<br />
　　 我觉得现在全球化也有很大的弊病，最大的弊病就是只有商品和资本的全球化，而没有人权标准的全球化。讲简单一点，就是什么可以向国际接轨，但是劳动权益不能向国际接轨。连印度都可以有独立工会什么的，中国就是不能有。这个和经济水平没有关系，多少比中国更穷的国家，公民的权利比中国更强，为什么中国没有？所以我觉得全球化是要提升的，它不能仅仅停留在现在这个层面上。</div>

							
							
							
							
