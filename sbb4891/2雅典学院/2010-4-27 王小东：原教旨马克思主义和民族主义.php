18世纪的法国开明贵族自己革自己的命,一方面革掉自己的特权,另一方面又追求普世人权,于是这些贵族与平民站在了一起,要求改革.这种权利向下渗透的过程,就是社会迈向平等的过程.这个过程是以皇权为中心向下渗透,或者以特权为中心.同样的道理,在人类文明的发展过程中,不仅有权利向下渗透,而且还有权利向外扩张,比如美国的废奴(把权利扩张到不同种族),妇女获得选举权(把权利扩张到不同性别),废除南方种族隔离法案(让不同种族之间的权利更加平等)等等,这个过程最初是以白人男子的权利为中心向外扩张的.到今天,他们已经是一个趋于平等的公民社会了.但是,权利向外扩张的过程并没有停下来,它会继续扩大到全人类.这就是为什么美国公民会组织游行,为远在万里之外的达尔富尔的基本人权问题表示关注.<br />
<br />
我们的区别可能就在于此.你认为这个过程已经停止下来了,权利范围就以国家为边界,而对外则是殖民的甚至野蛮的.而我却认为这个过程仍然没有停止.而且我相信有世界大同的一天,而要实现世界大同,就必须有共同的,最基本的普世价值原则.市场经济可以把世界各国从物质利益的角度联系起来,而平等自由则可以把各国从文化精神的角度联系起来.我一直认为,今天的欧盟和美国代表着人类文明的发展方向,前者代表着不同国家之间的融合,后者代表着不同民族之间的融合.而他们都有一个共同点,那就是以人权为核心的宪政民主+市场经济.</div>

							
							
							
							
