如果把普世主义定义成：不斗争（不激烈斗争）主义，那么它当然不会直接引发任何重大冲突。问题是，这个定义不对。就算是攻打伊拉克，也可以给它一个普世主义的解释。就算你把攻打伊拉克这种行为排除到普世主义以外，也不能说明普世主义就有利于化解冲突：像伊拉克这样的邪恶国家，如果留着不管，那肯定会诱发更大的冲突。<br />
