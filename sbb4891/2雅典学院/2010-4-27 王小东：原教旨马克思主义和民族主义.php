													<h2>王小东：原教旨马克思主义和民族主义</h2>
						
						
																											<div id="postmessage_585078" class="t_msgfont">柴荣是个马克思主义者，还是个原教旨的。他在他今天的博客写到：“最近义和团的事把我气糊涂了。。。。。有位朋友给我发了个邮件：我们不同意袁的观点，但是没必要引用王小东，尤其是冼岩的观点。尤其是后者，我认为我们应该与之划清界限。在这个问题上――应该说，在其他问题上也一样，不要意气用事，图一时的痛快。这样做容易让人产生误解。另，在这个问题上没有必要过深卷入。。。。在此感谢这位朋友。下决心了，以后不管义和团的事了。”<br />
 <br />
我想柴荣和他那位朋友的意思是说：马克思主义和民族主义是不相容的，所以要“划清界限。”理论上粗看似乎有点如此。马克思主义强调阶级利益，民族主义强调民族利益，它们的出发点就不一样。它们的不相容似乎很容易理解。可是真是这样吗？<br />
 <br />
其实，前苏联实行的社会主义就是表面是社会主义，实质是民族主义的,虽然它也宣称民族主义是形式,社会主义是实质。斯大林就说过民族利益大于国际无产阶级利益的话。不过，这可能不算数，因为柴荣是个原教旨的马克思主义者，他很可能并不认同前苏联搞的是真正的马克思主义。<br />
 <br />
那么，法国著名记者，学者，政客，切。格瓦拉的朋友，在玻利维亚被判了30年刑的Rigis Debray是不是原教旨呢？我不知道，但我可以引用他的一段话来看看他是怎么看马克思主义和民族主义的。Rigis Debray说：<br />
 <br />
The near-silence of European Marxists on the question of nationalism will one day be seen as the most costly and ruinous of all historical omissions. Such people work in a vacuum.(Letter of Regis Debray, 1969)<br />
 <br />
大概马克思当年和民族主义结下梁子是因为欧洲人的民族主义热情影响了工人阶级的阶级斗争的热情。民族主义的主要推动者是资产阶级，民族国家的建立使这些资产阶级成为受益者，使他们可以独享国内的市场，并且可以通过民族国家来保护他们在国外的利益。所以民族主义在马克思主义者看来是反动的，我想这大概就是原教旨马克思主义者反对民族主义的原因吧。<br />
 <br />
要是按着马恩的观点，南斯拉夫人的1848－1849年的民族运动是反动的，因为他们反对的社会分工相对明细的统治者，而这统治者在马恩看来是代表社会进步的力量。以上是历史事实，马恩确实表达了这样的观点，要真是象他们所说的那样，中国大概也不应该收回香港了。<br />
 <br />
常识告诉我，一个英国的工人对他的老板的认同，一定要大于对中国工人的认同。他们之间一定会比和中国工人之间有更多的共同语言。<br />
 <br />
马恩的问题是中了黑格尔的某些毒太深了。黑格尔说：存在的都是合理的。马恩的作法是先认为自己的理论是合理的，然后去设计存在。反对南部斯拉夫人民争取独立就是例子。黑格尔说有些人注定青史留名，有些人注定默默无闻，马恩就认为民族主义只是资产阶级的一厢情愿，可那些老百姓的想法没表达出来就不存在了吗？<br />
 <br />
马恩的局限还在于，他们过份重视人的物质需求，而忽视了人的精神需求，某种意义上说就是太过重视政治和经济的互动，而忽视了文化。这一点我在我的《中国民族主义》里面已经讲过了。民族主义，不仅给一个民族生存的保障，而且还给同一民族文化的人们带来很大的精神满足。这一点，原教旨的马克思主义挖掘得相当不够。<br />
 <br />
我说过多次，这世界就是个万花筒，人的视角本身也是个万花筒，怎么看都随人。但不管你怎么看，哪个角度最能反映大多数人想看的图景还真是不一样的。在我看，民族主义反映了当今中国乃至世界的大多数人们心中对这个世界的看法。原教旨的马克思主义者，我不能说你们错，我只能说你们是不切实际。</div>

							
							
							
							
