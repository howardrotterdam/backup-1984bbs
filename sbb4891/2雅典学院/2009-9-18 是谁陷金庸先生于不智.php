																											<div id="postmessage_247815" class="t_msgfont">7.<br />
与其他武侠小说作家相比，金庸先生是有思想，有思考的。在《神雕侠侣》中，他生动地展现了日月神教中对东方不败和任我行的个人崇拜和对教众的精神束缚。金庸说，他并不是在影射文化大革命。他还说，影射文学的意义是有限的。对于金庸先生的表白，我认为是一种名士的风度。<br />
<br />
金庸对于人性和世态的洞察，是以往的武侠小说家所不具备的。岳不群夫人宁中则的势利，黄蓉的庸俗，都刻画得很生动。同时，他不做评论，不加藏否，也不因此全盘否定书中的人物，堪称大手笔。我有时想，这么精彩的内容放在武侠小说里面可惜了。<br />
<br />
金庸试图在作品中引入新的历史观。在《天龙八部》中，通过萧峰的遭遇和内心的挣扎，让我们用另一种视角来观察宋辽之争。在当时，还算是有新意的。不过，这只是可供参考的一种观点而已。<br />
<br />
以后，大陆学界从国家主义和官本位出发，刻意抹杀正义与非正义、进攻与防御以及侵略与反侵略的界限，把宋辽、宋金和宋蒙战争定义为兄弟之间的战争。最终的结果，就是强权即真理。强者，掌权者的罪行和暴行，都是不能够谴责的。<br />
<br />
金庸的武侠小说试图改变人物非白即黑的惯例，人物和情节也因此而生动。不少英雄人物，比如杨过和令狐冲，都是有缺点的。也可以说，金庸在小说中没有特意地设置英雄人物。<br />
<br />
同样不幸的是，以后大陆出现了去道德化的过程。从黑白分明到在黑白之间加入灰色的过渡，再到模糊黑白之间的差别，最终实现了黑白的颠倒。金庸的小说，也成为道德相对主义的依据。<br />
<br />
金庸最喜欢的作品，是《鹿鼎记》。然而就是《鹿鼎记》，彻底颠覆了人世间诸如道义一类事物的追求。在一个新版电视剧《鹿鼎记》的推介节目里，《鹿鼎记》成为励志剧。嘉宾和观众讨论的，是学习韦小宝如何在险恶的政治和社会生态中获得成功，如何在对付七个老婆的时候游刃有余。当然，这不完全是金庸的责任。<br />
<br />
比《鹿鼎记》再进一步，就是张艺谋先生导演的电影《英雄》了。《英雄》告诉我们，侠客首先应该考虑天下，而天下属于强权。为了天下，侠客应该退缩，应该半途而废。<br />
<br />
回过头来看，武侠小说还是需要明确甚至简单的是非曲直，这也是社会伦理的组成部分。</div>

							
							
							
							
