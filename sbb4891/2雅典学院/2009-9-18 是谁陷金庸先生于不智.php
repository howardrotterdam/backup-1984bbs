																											<div id="postmessage_247801" class="t_msgfont">是谁陷金庸先生于不智<br />
刘松萝<br />
<br />
早就想写这个题目，后来放弃了。原因很简单，那时金庸先生还谈不上十分的不智。现在整理出来，也算是对他担任中国作家协会名誉副主席的反响。<br />
<br />
1.<br />
金庸先生的作品红遍香港、台湾和东南亚的时候，中国大陆还处在封闭之中。从缝隙之中进入大陆的金庸小说以其多彩和飘逸，让人们知道文学并不是政治的工具。因此，禁锢反而加强了人们对于金庸作品的渴望。<br />
<br />
金庸具有文学大师的潜质。我们甚至可以说，他的才华远在众人之上。忙于报业的金庸写出的连载小说，很多成为杰作。诸如左冷禅、任我行、周伯通、星宿老仙等人物虽然有些漫画化，还是给人留下了印象。金庸对岳不群的刻画更是入木三分，成为伪君子的代名词。《神雕侠侣》中杨过和小龙女的故事虽然有些矫情，那种山崩地裂的生死之爱令人难忘。记得读《神雕侠侣》，读到小龙女失踪后杨过的心情时，曾经感受到让人窒息的重压，以至于不敢再读第二遍。<br />
<br />
在政治或者商业占上风的社会里面，金庸的作品对于保留民间文化和民间社会的价值起到了重要的作用。有些很可能会被淘汰的词汇，比如武林、秘笈、宝典等等，至今仍然在社会上流行。词汇因作家而保留，足见金庸的影响之大。网络兴起之后，一部分词汇来自最新的词语，另一部分却是金庸式的词汇，比如版主、大侠等等。<br />
<br />
如果说有负面的效果，那就是金庸的小说加强了中国大学生乃至学者思维的非理性和非逻辑思维。不过究其原因，主要是他们把供娱乐，至多是供参考的武侠小说当成人生指南了。<br />
<br />
还有，金庸小说中频繁出现的“内力”、“神功”和“大发”等等，也对民间的迷信活动起到了推动的作用。<br />
<br />
记得中国文学界过去头脑发热，喜欢说这个人是中国的契科夫，那个人是中国的某某某。现在，没有人这样说了。这在一方面是务实，另一方面却是没有追求了。<br />
<br />
不过，假如说金庸是中国的大仲马，应该不算过分。</div>

							
							
							
							
