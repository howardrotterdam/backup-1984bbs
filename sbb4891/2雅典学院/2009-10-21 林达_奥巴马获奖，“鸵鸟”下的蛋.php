													<h2>乔姆斯基：永远的口头异见者</h2>
						
						
																											<div id="postmessage_299703" class="t_msgfont">袁晓明 <br />
<br />
<br />
<br />
近期，《南方人物周刊》对美国语言学者乔姆斯基做了专访，题目是“乔姆斯基：永远的异见者”，《南方人物周刊》在对乔姆斯基介绍中，引用了“最重要的知识分子”头衔。其实，在一次２万人参加的网上投票评选中，乔姆斯基当选的是“全球最伟大的知识分子”。<br />
<br />
公平地讲，《南方人物周刊》对乔姆斯基的介绍远远不够，英国的《卫报》称乔姆斯基著作与马克思、莎士比亚、《圣经》齐名。Robert <br />
Barsky在为乔姆斯基所写的传记中称，乔姆斯基对于我们的后代，就如加利略、牛顿、莫扎特对我们影响一样。美国娱乐界以及好莱坞的许多明星更把乔姆斯基几乎当成了他们的“上帝”。<br />
<br />
《南方人物周刊》称乔姆斯基为永远的异见者，没错，也许乔姆斯基的异见是永远的，但他的异见永远是口头上的，因此，对乔姆斯基的描述，更恰当的题目应该是“乔姆斯基：永远的口头异见者”，这也就是《南方人物周刊》没有介绍的乔姆斯基的另外一面。人无完人，乔姆斯基这位“全球最伟大的知识分子”也有鲜为人知的一面。<br />
<br />
毫无疑问，乔姆斯基是著名的语言学家，但他最著名的方面并非是表现在语言研究，而是乔姆斯基对美国的政府和制度的异见。具体地讲，乔姆斯基异见主要表现在两个方面：一是五角大楼（美国国防部）是世界上最邪恶的机构；二是美国的资本制度为万恶之源。<br />
<br />
我称乔姆斯基为永远的口头异见者，并非是指乔姆斯基用嘴在表达他的异见，而是因为他仅将异见表现在口头上，行为上却是做得完全相反，这当然不是我的发现，而是史坦福大学胡佛研究所的Peter Schweizer的一项研究成果。Schweizer在２００５年根据自己的研究，写了一本畅销书“按我说的去做，不要按我做的去做”- Do As I Say(Not As I do)。在书中，Schweizer用详细的事实（包括报税表）揭示了美国最著名的自由派人士的说与做的差异，其中有一个章节是研究“最伟大的知识分子”乔姆斯基。<br />
　<br />
Schweizer在“按我说的去做，不要按我做的”一书中对乔姆斯基有极大的称赞，Schweizer以不能否认的事实证明：乔姆斯基以他的一生建立起成功的事业，而他成功的事业就是在行为上完全抛弃他在嘴上所提倡的，简单的讲，乔姆斯基一生的事业就是印证了Schweizer的书名“按我说的去做，不要按我做的去做”。<br />
<br />
依据Schweizer的研究，乔姆斯基在一生中从五角大楼领了数百万美元的工资，但他的一生都在谴责五角大楼是邪恶的代表。乔姆斯基坚持大学教授们不应该为美国国防部工作，大学的研究机构也不能从五角大楼获得研究资金，因为最终会帮助五角大楼达到邪恶目的，即军事化美国社会。乔姆斯基还反对大学生去获得军方的奖学金，从越战以后，他就一直致力将美国军方的奖学金项目从美国大学校园赶走。可是，Schweizer却非常惊讶地发现，过去的四十年，乔姆斯基从五角大楼挣了几百万美元的工资。<br />
<br />
上世纪五十年代中期，乔姆斯基获得博士学位后，他进入麻省理工学院。乔姆斯基本来是搞语言研究的，他应该加入大学的语言系，但他却选择了在电子研究试验室工作，因为电子研究试验室有充足的资金，而这些资金主要来自于美国国防部和几家大的跨国公司。在电子研究试验室，乔姆斯基只承担非常轻的教学工作，拥有充足的下属，并领取比同级教授高３０％的工资。乔姆斯基用从美国军队来的研究资金，撰写了他的第一本书“句法学结构”（Syntactic <br />
Structures）。<br />
<br />
虽然乔姆斯基宣称美国大公司也是象法西斯一样，但他并不拒绝从柯达公司来的资助。乔姆斯基的第二本书“语法理论”（Aspects <br />
of the Theory of Syntax）也是美国军方出钱资助的。为什么五角大楼要资助乔姆斯基的语言研究呢？五角大楼明显不是对语言学的发展有兴趣，五角大楼用乔姆斯基的研究成果去改进军方的计算机系统，也就是说，乔姆斯基的研究直接用于美国的军事用途。<br />
<br />
就有关乔姆斯基从五角大楼赚取工资的事情，Schweizer给乔姆斯基去电子邮件询问，乔姆斯基不愧是大人物，他回了电子邮件：“我认为，我们应该对自己做的事情负责，并不需要去管工资单是谁盖的章”。显然，在铁打的事实面前，乔姆斯基也不能抵赖从五角大楼领了工资。<br />
<br />
为穷人、被压迫者、资本主义的受害者，乔姆斯基一生都在呐喊，但许多资本主义的受害者不知道的是，乔姆斯基是一个非常精明的资本主义者，并且是一个极有创新精神的创业家。乔姆斯基通过“邪恶”的资本主义系统，几十年为自己积累了几百万美元以上的财富，其中还包括乔姆斯基诅咒的“邪恶”的股票。按着乔姆斯基的身价，他是属于美国２％上限的富人。<br />
<br />
乔姆斯基特别强调阶级斗争，声称自己要和穷人站在一起，但他住在波斯顿郊区的富人区。如今，乔姆斯基这个与资产阶级斗争的斗士拥有的房子价值８５万美元，他还有一所用作度假的房子，价值１２０万美元。乔姆斯基住的地段全都是百万左右的大房子，旁边就是州立公园，这个地段不允许建多住户的公寓，因此，乔姆斯基所在的地段永远就不会有穷人住进来。<br />
<br />
对于美国富人利用信托基金逃税，乔姆斯基是严加谴责，但乔姆斯基以自己２百万美元的财富也建了一个信托基金，把他的女儿作为信托基金的收益人，他把书的版权也都放进了基金里面。Schweizer通过电子邮件与乔姆斯基询问信托基金的事情，乔姆斯基觉得冤枉：“为什么我不可以为自己的子孙存一些钱。”但乔姆斯基不解释为什么他要谴责那些也是为自己子孙存一些钱的人。<br />
<br />
事实上，乔姆斯基能够成为一个成功的富人，就因为他本来就是一个资本主义者。尽管他是那么反对资本主义，但他与其他公司一样建立品牌那样，他把自己打造成了美国以及国际知名的品牌，他到处演讲，每次收取演讲费１.2万美元，他也在网上提供演讲的录音下载，当然也不是免费下载。乔姆斯基的书更是从美国和国际市场上为他带来了巨大的财富。<br />
<br />
在９.１１之后，谁都不敢借机发财，但却是拦不住乔姆斯基，由于市场需求太大，他立即按照市场经济的规律将演讲费从９０００美元提高到１２０００美元，乔姆斯基更是及时地在几个星期内出了一本谴责美国造成９.１１的书，该书立即在美国以及其他国家成为畅销书，尤其是在国外销量看好。<br />
<br />
乔姆斯基认为美国税收制度太偏向富人，因为富人们可以用各种的方式逃避税收，其中一个方式就是为子女建立信托基金以便逃税，乔姆斯基以此谴责别的富人，可作为富人的他也以百万的财富为自己的孩子建立了一个信托基金。<br />
<br />
美国的国父杰佛逊等从一开始就相信，私人财产权是国民重要的权力之一，但乔姆斯基却认为私人财产权是富人的一个工具，他也认为知识产权也没什么两样，他指出，制药公司在花了巨大投资开发了新药后，不应该拥有专利。可乔姆斯基自己却对他的书、演讲等都有严格的知识产权的保护，在他的网站上对他的智慧都注有“乔姆斯基版权所有”的字样。如果没有他书面的同意，乔姆斯基网站上的东西是不能随便转载的。<br />
<br />
既然乔姆斯基能与马克思、牛顿、莫扎特等为人齐名，当然他有大量的追随者，但遗憾的是，一部分追随者领悟到“口头异见者”的真道，另一些人却是在盲目的崇拜。好莱坞的一些明星们应该是领悟到怎样在口头上追随乔姆斯基，他们每部电影的片酬要几百万美元甚至上千万，他们不会按乔姆斯基的教导去与那些跑龙套、打灯光的人领取同样的工资，在投资方面，他们也不会在乎把资金投资到“邪恶”的美国公司股票上面。<br />
<br />
一些美国的大学生却是在盲目崇拜，他们没有明白乔姆斯基“按我说的去做，不要按我做的去做”的真道，他们真地在大学毕业后不去美国资本主义的体系里赚钱。我有一位同事，他的儿子在美国的一所名校读了两年书后，开始追随乔姆斯基，认为美国有最黑暗的制度，同事的儿子并响应乔姆斯基的号召，离开大学后，一心想与美国的制度抗争，结果根本找不到工作，只好住在父母的家里，手机的费用都要父母支付，学生贷款的帐单来了，也都支付不了。最后，在父母的督促下，借着如今美国的经济好，找到了一个工作，才离开了父母的房子，也许同事的儿子现在知道了，乔姆斯基是要他们做口头上的异见者，而不要落实到行动上。<br />
<br />
其实，象我同事儿子那样的对乔姆斯基的追随还算幸运，乔姆斯基还曾号召不要向美国税务局交税，如果有年轻人响应了乔姆斯基抗税的号召，那是要进监狱的，美国政府可以不管你跟着乔姆斯基谴责总统是最大的罪犯，但你要抗税的话，那税警是会带你去税务局走一趟的。在抗税的问题上，乔姆斯基当然更是一个口头上的异见者，因为他是“最伟大的知识分子”。</div>

							
							
							
							
