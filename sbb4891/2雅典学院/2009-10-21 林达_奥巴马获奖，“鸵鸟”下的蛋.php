													<h2>林达:奥巴马获奖，“鸵鸟”下的蛋</h2>
						
						
																											<div id="postmessage_294627" class="t_msgfont">2009-10-19 11:16:00　来源: 南方网　跟贴 0 条 手机看新闻 <br />
<br />
<br />
<br />
　　这个颁奖折射了欧洲引领的一厢情愿态度：对9・11以后世界局势本质变化采取鸵鸟政策。无力直接切入问题核心并且正视它，而是绕开危机，把自己留在虚幻的道德完美梦境之中。<br />
<br />
　　&nbsp;&nbsp;<br />
<br />
　　奥巴马获和平奖的消息传出后，《时代》杂志网站刊登了一组经过PS后的恶搞照片，把奥巴马“P”上了各大颁奖典礼的领奖台，其中包括MTV音乐奖和奥斯卡金像奖。今年诺贝尔和平奖桂冠落在美国总统奥巴马头上，出人预料。一开始记者打电话去白宫，回话说大家还都傻在那里，议论都没人议论。在接到消息和奥巴马公开回应的几个小时里，外界甚至估计他可能会拒绝领奖。奥巴马是个演讲天才，可这次是我听到的最糟糕总统演说。就连前面必须捎带的轻松笑话都是快速低调匆匆扫过。感觉只是尴尬：一点不想念这份稿子又非念不可。<br />
<br />
　　诺贝尔奖委员会称奥巴马得奖主要是外交的和平努力，除了表示惊讶，奥巴马说“让我讲讲清楚，我没把它看作是对我个人所做事情的一种承认，我更愿意认为，它是代表了怀着期望的各国人民对美国领导层的肯定。”说得非常牵强。消息一出，不少评论指出，这个颁奖，委员会是在战争与和平问题上，以褒奖奥巴马，来表达对布什时代的强烈不满。所谓“布什遗产”确实值得好好反省，因为美国在这一阶段出现明确拐点。其中首当其冲的是伊拉克战争。关键在于，如何批评、检讨和反省。<br />
<br />
　　例如对伊拉克战争多年来盛行猜测动机的臆断式批评。谴责之一是：布什推行单边主义以至不惜以战争夺取石油。现在谜底揭晓：战后伊拉克石油由新**公开招标，第一次竞标得主是中国。批评针对的是批评者的假想，和伊战没有任何关系。有意义的检讨是：在布什时代，世界究竟面临了什么重大挑战？假如布什应对是错的，他错在哪里？而欧洲和国际社会，拿出了什么更好的应对方式？奥巴马又怎样改变了这个危险世界、促进了和平？<br />
<br />
　　布什时代的开端是9・11事件。恐怖组织以19个非军事人员，瞬间造成超过二战期间几乎摧毁整个美国海军的珍珠港战役之人员和物质损失。袭击后续计划不可预测，造成前所未有的战时反应：不仅美国**全部关闭转入地下掩体办公。联合国总部、位于法国的欧洲议会总部、比利时首都的北约总部，同时宣布紧急疏散。北约随即宣布在历史上第一次启动北大西洋公约第五款，也就是共同防卫机制。之后，北约五架预警飞机到美国本土报到，执行巡航任务。同样攻击是否会再次发生，在哪里发生，至今没人知道。<br />
<br />
　　联合国根据二战经验建立，目标是预防国与国之间常规战争。联合国安理会以采用外交斡旋、经济制裁、派出维和部队等一系列“集体办法”遏制战争发生。而9・11显示，恐怖战争可能使用任何被联合国禁用的武器，无人道底线、无视战争法和任何规则，无谈判责任方。其无预警、大战役式毁灭能力，击垮了列为《联合国宪章》作为宗旨的第一条：“采取有效集体办法，防止且消灭对和平之威胁。”联合国应对常规战争的预防性救援，在恐怖战争面前完全失灵；长期维护国际安全的机制在新威胁前彻底失效。以上是布什时代美国和世界面临的共同挑战。<br />
<br />
　　之后引出分歧的“布什突破”是：从国家安全计，在联合国安全机制无力应对恐怖战争的现实面前，认定受威胁国家有权“自行执法”。美军入侵阿富汗是对国际关系观念的本质突破：按原来规则，恐怖分子是罪犯，只能通过国际刑警组织“捉拿罪犯”，而不是对主权国家出兵入侵。在恐怖战争定义下，本・拉登不是罪犯而是战争敌方，有意协助他的塔利班政权是敌方同盟。同时，基于恐怖战争的无预警、瞬间大规模杀伤的特点，布什提出“先发制人”对策：对支持恐怖主义、拥有大规模杀伤武器并且不顾忌使用的危险国家，先发打击以防范自身安全。这是对二战后国际秩序的进一步突破。<br />
<br />
　　“自行执法”显然是下下策，隐含国际新秩序危机：各国借口受威胁而轻易发动战争怎么办？对威胁程度的判断错误（例如情报错误）怎么办？<br />
<br />
　　所以，布什时代世界面临的挑战应该是：联合国原有机制无力应对新型恐怖战争，国际社会如何推出新的安全机制？这一挑战从联合国前秘书长安南责成专家组拿出办法，至今没有结果。争议是：在尚未推出有效新机制之前，受威胁国家是否有权“自行执法”？如果判定它有权去做，天下大乱怎么办？判它无权去做，让它坐等9・11式的攻击、坐以待毙不成？这个问题非“美国专利”，欧洲亚洲都在恐怖战争威胁下。<br />
<br />
　　事实上，国际社会没有拿出应对方案，只是要求大家回到、固守联合国原有体系。这个颁奖折射了欧洲引领的一厢情愿态度：对9・11以后世界局势本质变化采取鸵鸟政策。无力直接切入问题核心并且正视它，而是绕开危机，把自己留在虚幻的道德完美梦境之中。<br />
<br />
　　奥巴马确实改变了布什时代美国和国际社会、主要是欧洲的紧张关系。奥巴马的方式是什么呢？他在表面上顺应欧洲想法：例如一边向阿富汗增兵、打着反恐战争，一边宣布美国再不用“反恐战争”这个词。他宣布要和伊朗、朝鲜通过谈判解决核威胁，而实际上朝鲜和伊朗都在按照原来计划行事，导弹射程越来越大，核研发逼近临界点。美国并非受威胁最大国家，奥巴马和其他国家一样，有拖延回避余地，可是一旦伊朗核试验超过临界点，第一个在核威胁下的以色列就可能“先发制人”袭击伊朗核设施。<br />
<br />
　　对于伊战，奥巴马大张旗鼓宣称要16个月提前撤军，上台后又务实地严格按照布什签下的《美伊安全条约》实行三年撤离计划。原因非常简单，2003年是否要打掉萨达姆政权，是有选择的；萨达姆倒台之后的撤军问题没有主观选择余地，时间表被客观条件限死。2003年在伊美军和伊军组成联军，共同应对袭击伊拉克平民的恐怖组织，美军必须完成训练友军任务、等到伊军有能力单独应对，才可能撤军。这个时候呼吁“反战撤军”是对伊拉克不负责任。这个逻辑民众可能绕不过来，反战6年的欧洲领袖和知识界，应该很清楚。<br />
<br />
　　奥巴马一上台就高调宣布要关闭关塔那摩监狱，但实际上关闭监狱不等同于释放全部囚犯。他只是准备和国际社会一起回避讨论恐怖战争时期的法理难题：关塔那摩囚犯大多是阿富汗战争抓获的恐怖组织成员，那么，反恐中抓获的是“战俘”还是“罪犯”？放，9・11事件19个非军事人员的破坏能量大家都看到了，不放？法理依据在哪里？哪有“战俘”一直关下去的道理？按照“罪犯”刑事判罪？他们只是战场上的恐怖组织战斗员，够不上刑事判罪条件。这也是以色列长期关押大量巴勒斯坦武装组织成员的原因。<br />
<br />
　　假如国际社会有能力正视布什时代的真挑战，诚实面对，共同拿出高于布什的中策、上策来，才可能幸存于这个危险的世界。假如只对抢占道德制高点感兴趣，一味回避真问题，或许可以得到诺贝尔和平奖，却难以真正维护世界和平。（林达，知名旅美作家）<br />
