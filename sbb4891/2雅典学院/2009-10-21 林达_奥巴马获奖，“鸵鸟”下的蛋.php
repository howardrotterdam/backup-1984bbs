													<h2>电视狐好上，奥巴马难下 / 吴澧</h2>
						
						
																											<div id="postmessage_297129" class="t_msgfont">2009-10-23 01:16 | 阅读(756) | 标签: 奥巴马, 福克斯, 言论自由 | 字号：大 中 小 <br />
 <br />
<br />
奥巴马最近有点骑“狐”难下。<br />
<br />
 <br />
<br />
<br />
这一阵美国电视有部大戏，叫作“白宫大战福克斯”。某些白宫官员跟美国四大电视网之一的福克斯（ＦＯＸ）干上了，他们指责福克斯电视网的报道不公正。白宫联络处任阿妮塔・邓恩说，将要像对待反对党那样对待福克斯。虽然政府官员的矛头只是对着福克斯，但别的电视网自然也很感兴趣，必然要报道报道，评论评论。一时看上去，就似乎成了整个美国电视界的大戏。<br />
<br />
 <br />
<br />
<br />
其实，上段里的“报道不公正”，是本人按中国习惯的总结。要是按美国习惯说起来，里面的道道还要复杂一些。就是邓恩也说，福克斯专职跑白宫的记者梅杰・加勒特是个公正的记者。就是说，报道本身并没有什么不公正；问题是福克斯的评论总是抓白宫痛脚，特别是共和党的脱口秀重炮格伦・贝克，今天说民主党的健保改革让重病老人只能等死，明天说奥巴马是个歧视白人的种族主义者，令邓恩女士非常气愤。她制订了反击措施。据本星期一（１０月１９日）出版的《时代》杂志，奥巴马批准了邓恩的计划。<br />
<br />
 <br />
<br />
<br />
只是美国有言论自由，电视脱口秀主持人爱说什么说什么。福克斯负责人的回应比较客气，仅批评白宫将新闻报道和时事评论混淆了。新闻报道要求真实和公正；时事评论是评论者的个人意见，可以有自己立场。福克斯的脱口秀频道就不客气了，讽刺邓恩等人是“我所见过的最大一群哭宝宝”。<br />
<br />
 <br />
<br />
<br />
这次大战，甚至和中国有点关系。贝克在抨击邓恩时，挖出了她今年６月对一群中学生的鼓志演讲，其中提到毛择东是她最佩服的两个哲学家之一（另一个是德蕾莎修女）。邓恩说她欣赏毛在１９４７年所说的 You fight your war, I'll fight mine。这应该是指《毛择东选集》第四卷中重阳军会于该年４月２２发给晋察冀前线一封电报里的话，“你打你的，我打我的”。<br />
<br />
 <br />
<br />
<br />
其实白宫也不能把福克斯怎么样。已经被人称作“哭宝宝”了，要是再诉说自己感情被伤害，要求贝克道歉，或要求福克斯开除贝克，只怕不但福克斯，所有电视网的脱口秀主持人都要带着奶瓶上台讲笑话。最多也就是奥巴马要向全国民众直接推动健保法案时，他上别的电视网却不上福克斯。这算新闻节目，白宫不给福克斯做新闻，不当福克斯是新闻机构。但邓恩表示，奥巴马仍会接受福克斯的采访。采访就是主持人请总统就国际国内各类时事谈看法，白宫仍然承认福克斯有做评论节目的权利。<br />
<br />
 <br />
<br />
<br />
奥巴马的内阁里有很多老将，但他的白宫工作班子（相当于我国的国务院办公厅）多是他的年轻朋友，经验相对不足。邓恩女士于今年５月加入白宫团队之后，人们都说她干得不错。这位大妈把一群毛头小伙子管起来了，乱说话的事少了很多。这回大概是被成功冲昏了头脑，潜意识里有点要把言论控制扩展到白宫之外；再说，下属的年轻气盛难免会影响到她的情绪，对福克斯的攻击有点搏出位，不合美国惯例。<br />
<br />
 <br />
<br />
<br />
原共和党总统里根的撰稿人佩洁・努南批评奥巴马的班子搞不清竞选和执政之不同。邓恩的履历，也确实都在竞选一边，她一直是为民主党参议员和总统候选人工作。但竞选是有对手的；而执政是为全体美国人服务，其中包括反对你的人。这里有关键区别。<br />
<br />
 <br />
<br />
<br />
或许有所预见，邓恩发飙之前十来天，《纽约时报》专栏作者戴维・布鲁克斯在１０月２日发了篇文章，专谈贝克之流的影响。他说某些民主党人混淆了听众和选民的区别，夸大贝克们的能量，客观上为他们的极端言论推波助澜。布鲁克斯举了去年大选的例子。在共和党初选中，这些脱口秀主持人都是反对比较温和的麦凯恩的，他们要求共和党选民推选更保守的候选人。结果呢？麦凯恩胜出。美国的选民很成熟，听了极端言论，也就是乐乐，该干什么还是干什么。连共和党选民都是听其言而不践其行，民主党着哪门子急啊？<br />
<br />
 <br />
<br />
<br />
邓恩所欣赏的毛择东的那两句话，作为完整的军事策略来讲，下面其实还有两句：“打得赢就打，打不赢就走”。或许，现在该是邓恩女士全面地、准确地理解毛择东思想的时候了。<br />
