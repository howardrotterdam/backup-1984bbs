													<h2>LZ提到的这篇文章《中国现状分析：挤不进去，你永远是穷人》</h2>
						
						
																											<div id="postmessage_501530" class="t_msgfont">第一节 国民收入的半数供养政府工作人员<br />
<br />
中国需要老百姓供养的政府工作人员究竟有多少？这绝对是一个世界性的科研攻关项目，我国政府有关方面的统计是“截止2003年底,中国公务员的总数是636.9万人”，请无论如何记住一点，在政府机构的人员中，公务员所占比例并不大，2005年官方非正式估计“中国吃财政饭的人员约有4500万人以上，另外，还有500万人仰赖于政府赐予的权利实行自收自支”。　　<br />
<br />
　　　　<br />
<br />
我认为财政饭有三层意思，第一种财政饭是国家法理许可的财政拔款，比如公务员的部分帐面工资就是合法的吃“财政饭”。第二种财政饭是政府有关规定赋予的“自收自支”权利而获取的，如工商，城管等部门。第三种财政饭是法理法规皆不许可。但是却被社会完全默认的财政来源，也就是每一个政府部门皆在干着的乱收费乱经营搞实业拿干股，这个是政府默许的潜规则范围内的“财政饭”，而这个财政饭要比合法的财政饭大得多。　　<br />
<br />
　 　<br />
<br />
有一点要指出，以上三种政府收入皆是被社会默许的财源，与贪污受贿并不是一个概念。　　<br />
<br />
　　　　　　<br />
<br />
这些数据就能够大致得出中国老百姓需要赡养的人员总数了吗？绝对不是，我可以举几个例子以证明：我所在的国有企业（可以依此来类推政府情况），几乎每一个部门每一个办公室都有“借调”人员，什么是“借调”人员？就是不属于编制内，但实际就是靠他们干活的员工，这些人都是隐身人，是不在正常的统计报表内的，但是数量惊人。再举个特例，咱以前在城市街头经常可以看到一辆小型客货两用车突然出现，上面跳下来几个穿制服的城管队员和不穿制服的农民工，一阵鸡飞狗跳后，在城管部队的指挥下，农民工将缴获的战利品搬上汽车扬长而去，大家注意了，城管部队是法律不承认，但是可以吃部分财政饭的人员，而这种农民工就是俗称“白员”中的最贱层。高级的白员是什么人？是小有头面的小官员的家属们，他们被安插在政府各衙门里，活得绝对不比一般政府小职员差，还有一种非常特殊的拟似“白员”群体，比如交警部门的暂扣车辆停车场，很多（我不敢说全部）交警部门的停车场都是由地方上的地痞流氓“管理”的，你可以疏通他们来要回自己被扣的车辆，这些人实际上也是一种非常特殊的“白员”。　　<br />
<br />
　 　<br />
<br />
我举这些例子目的很简单：中国除了包括600万公务员在内的，在党政机关中“国有”身份的员工1000余万人，以及科教文卫金融等等事业单位3000余万员工外，政府机构里还有很多很多靠“合法”或非法的财政吃饭的人，这些人的数量甚至比公务员的数量不知要多了几倍。其实中国那些行业协会也是属于吃自收财政饭的群体，因为它们之所以能够有威慑力来获取利益还是靠政府这张老虎皮给予的权力。　　<br />
<br />
　 　<br />
<br />
　 　<br />
<br />
我为什么将合法二字加“”号？因为我已经无法分清中国这个国家里什么叫合法什么叫不合法了。　　<br />
<br />
　 　<br />
<br />
　 　<br />
<br />
现在大家应该有个大致的印象了，中国除了四千多万国家承认的吃财政饭的人员外，还有数量惊人的属于潜规则范围内的吃财政饭的人员，咱实在没有能力得出他们的实际数量，我想，一两千万人总是要有的，这个数字我认为是保守的。　　<br />
<br />
　　　　　　<br />
<br />
好几千万人的吃财政饭大军需要多少财富来供养？还请注意，农村这块地方还没计算呢，根据官方数字，1995年，乡、镇、村还有吃三提五统的1400万人，还不包括700万村官，又说是500万村官，我就不明白了，政府怎么连自己的职员都统计不清楚？经过改革，现在也许少了，我只能说但愿如此，不交农业税后难道要把这些人饿死吗？用多少钱来供养政府职员这又是一个科研难题。根据官方公布数据，2007年国内生产总值246637亿，2007年全国税收收入完成近五万亿元，而根据中央　党校　教授、知名学者周天勇的估计，中国2007年实际财政收入达到9万亿元人民币。　　<br />
<br />
