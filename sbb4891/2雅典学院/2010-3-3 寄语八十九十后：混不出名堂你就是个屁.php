刚刚看到个帖子，转过来：<br />
我也在研究怎么让自己幸福。<br />
　　 <br />
　　 印度佛学告诉我们，人的痛苦来自于欲望，他提倡消灭一切欲望以求得心灵的平静才能得到幸福。 <br />
　　<br />
　　 西方哲学告诉我们，幸福=已满足的欲望/欲望。幸福取决于消灭分母“欲望”。 <br />
　　<br />
　　 马克思告诉我们，当人类全住在矛房时，本没有高低贵贱与贫富之分。经济发展后，一部分人住进了砖瓦房，另一部分人住进了高楼大厦。人类才产生了贵贱与贫富之分，苦恼随之而来，幸福也将不在。 <br />
　　<br />
　　 海德格尔告诉我们“向死而生”，人在出生和死亡时都最大限度地接近灵魂的家园，“向死而生”大彻大悟客观求实的追求灵魂的呼唤，才能实现最高幸福。 <br />
　　<br />
　　 马斯洛告诉我们，自发的坦率地真实表现自己，达到自我实现，追求人性中的真、善、美，导向塑造完美人格，实现最高幸福。 <br />
　　<br />
　　 绝大多数中国人则认为：幸福=不停的吃喝玩乐升官发财。唯一的苦恼来自于周围有比他吃喝玩乐升官发财更厉害的人。<br />
　　<br />
　　 所以，大部分中国人是痛苦的，因为他的幸福观跳不出一个“钱”字，钱多幸福，钱少不幸福。事实是，不管给他多少钱，他都不幸福。<br />
　　<br />
　　 我看见那些为钱少而感到不幸福的人，逐渐仇视比他富有的人，逐渐习惯于从阴暗的一面看待客观事物，逐渐为自己没办法使用阴暗的手段而感到愤愤不平。乍一看，正义感很强，实则让他做了官，比鬼还要贪。<br />
　　<br />
　　 他的思维领悟不到：只要是钱能解决的问题就不是问题，而那些不能用钱解决的问题，恰好是一生的痛。今天没钱，并不代表明天没钱；一年没钱，并不代表一生没钱。基本上做到“非文盲、肯努力、愿学习、不偏激”，钱是很容易赚的，取决于生活态度，取决于怎么看待客观事物。<br />
　　　　<br />
　　　　 改革开放前有穷人富人吗？没有，大家都住在矛房里，我父母有很好的职业，家庭条件算是所有亲戚中最好的。可我小时候只有过年才能吃上一颗糖，穿的衣服是哥哥穿过的。<br />
　　<br />
　　 改革开放后，再差的人也住进了砖房，可却产生了穷富。幸福的人很高兴以前住茅房，现在住砖房。不幸福的人则坐在大厦10层的地板上，痛苦地仰望着住在大厦第11层的人。<br />
　　　　<br />
　　　　 我们都有自己生活的圈子，毫无疑问，如果圈子中的某个人结婚，别人都送“200”礼金，而我们却因为相对贫穷，只能送得起“10元”，这将严重伤害我们的自尊，让自己感觉不幸福。<br />
　　　　<br />
　　　　 吃穿住行各个方面都是相同的道理，如果比的结果是自己处于送“10元”的消费，肯定会让人不幸福。<br />
　　　　<br />
　　　　 那么有办法避免吗？有！学会控制自己的生活圈子，让自己幸福。<br />
前不久，内地的几个老同事先后对我说，如果他能月入1万，他就很会幸福与满足了，我笑，说不可能的。他拿出了N条理由，证明自己真的会很满足、很幸福。<br />
　　　　<br />
　　　　 我是这样回答他的，你现在是月入2000，你会想“因为我不是很贪的人，假如我能月入1万，我肯定会幸福与满足”，没错，以前我也这样想的。<br />
　　　　<br />
　　　　 可当你真的月入一万时，你会发现周围所有人几乎全月入一万，并且还有很多人月入5万10万。这时，你又会想，“因为我不是很贪的人，假如我能月入10万，我肯定会幸福与满足”。<br />
　　<br />
　　 可当你真的月入10万时，你会发现周围所有人几乎全月入10万，并且还有很多人月入50万100万。<br />
　　　　<br />
　　　　 你永远都是“送10元”的人，你永远都会陷入无休止的苦恼，赚多少都觉得自己是穷人，觉得自己不幸福。<br />
　　　　<br />
　　　　 23岁时，我做国企员工，月入400，郁闷不已，穷啊，周围的人都是公务员之类的好工作，月入1000，那时候我想，这生活过不下去了，我是那“送10元”的人，我的尊严受到了损害。<br />
　　　　<br />
　　　　 25岁时，我做公务员，月入1000，郁闷不已，穷啊，周围的人都做生意，月入5000。那时候我想，这生活过不下去了，我是那“送10元”的人，我的尊严受到了损害<br />
　　　　<br />
　　　　 28岁时，我辞职做工作室，月入2万，郁闷不已，穷啊，周围的人都开公司，月入5万。那时候我想，这生活过不下去了，我是那“送10元”的人，我的尊严受到了损害。<br />
　　　　<br />
　　　　 29岁时，我做公司，月入5万。再也不傻了，热烈拥抱以前的老同事与普通白领，在这个圈子中，我能“送200”，但我不送，也和大家一样“送10元”。<br />
　　<br />
　　 这时，我才成了马克思定义中的富人，永远住在了大厦的最高层。其实，我们每个人都可以做富人的。古人云“知足者富”，旧社会的地主可比我们穷多了。<br />
　　<br />
　　 现在，几乎每天都有月入6位数的朋友叫我去玩，尽管我有资格溶入这个圈子，但我根本就不与这个圈子中的人交往（工作除外）。<br />
　　<br />
　　 因为我不是圣人，我知道别人“送200”而我只能“送10元”会很不幸福的。如果继续溶入这个圈子，那么这个圈子一定会有一大批月入7位数的人，把我变成住砖瓦房的“10元”穷人。<br />
　　<br />
　　 一、控制自己的生活圈子。<br />
　　<br />
　　　　 二、别去想什么别人住在大厦的第几楼，所谓知足者富，只要以前住茅房现在住砖房，现在比过去好就行了。<br />
　　<br />
　　 三、金钱的唯一终极目的就是获得更多自由，不要本末倒置，用金钱来追求虚荣，限制了自身的自由不说，还会让自己永远很痛苦，因为你圈子里的人永远都有比你消费高的，追不到头。<br />
　　<br />
　　 例如：我走累了，可以自由的坐在草地上休息，因为我从不心疼身上那条几十元的裤子。你穿着价值几万的西装能自由的坐下吗？脚很累吧？哈哈<br />
　　<br />
　　 例如：我从不担心开车压死人，从不担心别人划花、盗抢我的车，从不担心找不到停车位，也从来没受过交警的侮辱，因为我没车。有车的你是不是成天担心这些？是不是对交警恨之入骨？<br />
　　<br />
　　 在家中花10元吃的一碟猪肉，与在豪华宾馆花100元吃的一碟猪肉，并没有本质上的区别。当你发现过多的物质消耗其实是一种负担，当你抛弃虚荣朴实地生活，当你开始追求自由，而不是追求在他人面前显得更高贵，过得更幸福之时，幸福就离你近了很多。</div>

							
							
							
							
