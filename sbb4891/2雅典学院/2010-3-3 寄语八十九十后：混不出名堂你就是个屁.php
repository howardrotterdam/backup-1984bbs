													<h2>寄语八十九十后：混不出名堂你就是个屁</h2>
						
						
																											<div id="postmessage_500499" class="t_msgfont">===========================================<br />
对于部分思想活跃的在校大学生的疑惑，以此文解答之：<br />
===========================================<br />
<br />
网络上精英粪青的嘴仗打个不停，其实除了极少数人因为生活在王族或优雅家族里，“开窍”的比较早之外，大家基本上都是从粪青转化过来的，记得第一次海湾战争开战时间是在接近中午的时候，导致的结果是当时我那个中学整个高中班都自动停课了，大家狂热得一塌糊涂，刚开战时大家还谈不上支持谁反对谁，就是吃了药似地兴奋，过了几天就一致支持萨达姆同志了，包括我在内，反正我没有发现谁支持美帝，不然揪出来肯定要批斗的，后来萨达姆自己宣布取得伟大胜利，我们很伤感，谁着时间的推移，我们“粪”的色彩越来越淡化。<br />
<br />
八十九十后注意一下现实中周围的群体，那些混得好些的群体，有几个没事泡论坛的？讲得直白点，喜欢耗论坛的群体中相当部分都蹉跎得很，前途渺茫不能公款娱乐，上点层次的圈子挤不进去，所以只好激扬文字评点江山了。<br />
<br />
有人肯定不喜欢我这个论点，他们可能收入还算不错，怎么就蹉跎了？其实大家都得承认一个现实：在中国，“混”得好远远不是收入还算不错就行的，混得好是指在社会上吃得开，比如生病啦上学啦交通事故啦等等，你能轻松搞定，能到哪里都可以打通关系，玩什么不用自己付钱，这才叫“混得好”，不然虽然收入不错可是苦得像个孙子，出门就摸黑，那只是小白领，算不上混得好，恐怕有时候活得还不如一个流氓痞子辅警顺畅呢。<br />
<br />
再谈谈理想与现实，网上左右之争，民主自由的热闹得一塌糊涂，其中那些带头大哥们，其中那些名人们虽不能说都是富裕人士，但至少也是有房有车族，他们多数人上网和他们的事业有关联，上网也是工作，各位是不是都有房有车了？是不是上网不影响你的工作或者说可以促进你现实中的工作了？我估计基本上是负值的。<br />
<br />
说到这里，我要特别指出的是，在中国要想混得还算不错，你肯定要拥抱体制，谁都不要装清纯，体制拥有一切，谁远离它谁就蹉跎去，当然蹉跎的人绝对不等于他就是高尚就是主动远离体制的，基本上是因为他没有能力去拥抱体制，大家可以搜索“挤不进去，你永远是穷人”这篇文章，我觉得我说得很清楚，只要生活在中国，拿的是中国国籍，不管是谁，不管他现在是高尚的还是卑鄙的，只要他过得舒畅，那么他肯定拥抱体制了，区别仅仅在于轻重，仅仅在于恶心程度，比如我们尊敬的秋雨同志，他的口味实在是太重了。<br />
<br />
在虚拟网络中再怎么高尚和激扬，文字再怎么优美和深奥，现实中该吃饭还是要吃饭，该进步还是要进步，每一个人都渴望得到社会的认可，得到家人、亲人、朋友和熟人的尊重和敬仰，那么你拿什么来获得这种认可？无非是“混”的能力，无形的“混”的能力就是你帮人办事的能力，有形的“混”的能力就是房子、车子、票子、女子，你能拥有这些，说明你在现实社会中混得好，混得好，说明你融入社会了，融入社会了，说明你顺应潜规则了，顺应潜规则了，说明你和体制拥抱了，和体制拥抱了，说明你在网上激扬文字的理想主义不过是一堆废话，虽然很无奈。有几个人敢不和体制拥抱？至少绝大多数人不敢拒绝体制，有决然气质的人毕竟是极少数，在今天这个欲望社会里更是沧海一粟。<br />
<br />
齐家，治国，平天下，我们的传统虽然很虚伪，但是这个排序倒是很实在，如果不能齐家，又如何能够治国平天下？如果不能让家人获得优雅的生活，那又有什么能力让国家优雅起来？比如海瑞同志。但是反过来，如果拥抱体制让家优雅起来了，那国家又如何能优雅起来？比如张居正同志。所以，中国的事情是绝对两难的事情，没有人能解决得了，但是有一点可以肯定，如果你不能让家优雅起来，那什么都不要谈。<br />
<br />
所以不要被迷惑，混不好，你就是个屁。<br />
