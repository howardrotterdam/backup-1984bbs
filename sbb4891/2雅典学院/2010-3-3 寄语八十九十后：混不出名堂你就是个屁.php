<br />
　 　<br />
<br />
这些蛋糕怎么分配我不知道，但我可以提供其他一些数据以分析：“八五”期间，公车车辆消费占到全部国家财政支出的38%，2003年国家财政收入约为 21715亿元，而由国家财政、预算外资金、企业和村民交费和列支成本支出的行政事业供养费用高达14266亿元，加上公安和武装警察的1500亿，达 15766亿元。占2003年GDP的13.52%，由这些被供养的公务人员所消耗；国家预算收入、有统计的预算外5000亿收入、没有被统计的3000 亿预算外收入，整个国家总计支出37960亿中的37.58%，由被供养的行政公务人员所消耗，而中国官员公车消费公款吃喝公费出国年花费达9000亿元，这都是官方的数据。　　<br />
<br />
　　　　　　<br />
<br />
从以上的官方数据，我们可以看出，国家财政收入的大部分都被政府人员消耗掉了。　　<br />
<br />
　　　　　　<br />
<br />
公布了官方的数据，现在我要谈谈自己的看法了，以官方认可的六百多万金牌公务员为例，这些人一年实际要花多少钱供养？中组部原部长张全景说：政治弊端是“ 官多为患”，一个省有四五十个省级干部，几百个乃至上千个地厅级干部，一个县几十个县级干部。中国共有33个省级行政单位（扣除台湾），2,861个县级行政区划单位。那么由此推类：全国各地就至少有一千多个省级干部，两三万个厅级干部，好几万到十来万个县级干部，至于京城及军队有多少高级低级干部我就不能得知了。　　<br />
<br />
　 　<br />
<br />
　　　　　　<br />
<br />
现在沿海地区政府部门里一般职员年收入十万元是很平常的，很多发达地方的政府普通公务员年收入二十万元也极普遍。注意，这是合法的收入，是基本可以拿出来晒工资的收入，养一个公务员绝对不止薪水这一块，医疗交通吃喝拉撒贪污受贿等等需要供养的地方太多了，可以说，国家除了没给政府职员配备法律意义上的配偶外，其它都是必须提供的，举些小例子：比如很多即使是普通公务员的家庭基本上都是不用开火做饭的。现在高调进行公车改革，交通费也给了公务员们，可是来来去去还是都有车子接送，现在政府职员每月的车贴就比民工辛苦一个月的薪水还要多。只要是稍有点权力的公仆，他们家庭外出海吃海喝哪个是自己买单的？说个笑话，我认识的政府人员中买个床单都要开个发票去报销。在很多地方，你做几年公务员后要是不搞个几套房子那简直是丢人。至于那些村王乡王县王市王省王等等的书记及某某长们，这些人及其家族一年需要多少财富来赡养？简直没法想象。　　<br />
<br />
　　　　　　<br />
<br />
废话说了这么多，大家自己可以估算一下：一千多个省级干部及其家庭一年要花费多少钱供养？两三万个厅级干部及其家庭需要多少钱供养？好几万到十来万个县级干部及其家庭又需要多少钱供养？再统计全国好几百万金牌公务员又需要多少财富来为他们全方位服务？　　<br />
<br />
　　　　　　<br />
<br />
党政机关其他几百万上千万人员呢？还有三千余万事业单位人员，还有几百万村官一千多万乡村混饭吃的人员，还有千百万个政府雇佣的形形色色的白员。党政机关其他几百万人难道就是省油的灯？事业单位难道就是后娘养的吗？你以为街道办事人员收入就低？我可以非常负责任地告诉你，很多地方街道的工作人员月薪万元根本不算什么，不信你去调查。　　<br />
<br />
　　　　<br />
<br />
　 　<br />
<br />
我可以说，中国国民收入的至少半数以上都装进政府人员的口袋里去了。　　<br />
<br />
　　　　<br />
<br />
中国就是一个超级二元社会，一部分相对数量很少但是绝对数量很庞大的中国人早已过上发达国家水准的生活，而其他中国人则是标准的贫穷中国的国民。　　<br />
<br />
　 　<br />
<br />
　 　<br />
<br />
第二节 主要经济组成部分是由特殊阶层操纵的非正常经济<br />
<br />
　　　　　　<br />
<br />
中国的经济组成笼统地说是由公有经济、外资和私有经济组成，但是中国的公有私有经济形式已经完全变味，公有经济未必公有，私有经济未必私有，实际上皆是由特权阶层操纵，它们是由特权阶层操纵的非正常经济，在我们意识形态浓厚时期称之为官僚资本主义经济。　　<br />
<br />
　　　　　　<br />
<br />
公有经济的最主要表现形式是垄断国有企业，这种垄断形式有两种，一是电信石油之类的在国内外仍然有一定竞争对象的企业，另一种是水电气之类的完全垄断的经营实体。　　<br />
<br />
　　　　　　<br />
<br />
曾经几时，公有经济实体里的职位还是一个鸡肋，大家是以其为耻的。而现在它们却是香饽饽，电信石油烟草之类靠着高度垄断和半执法者的地位以超烂的管理却获取惊人的利润，而水电气之类的经营管理者更是以狂赌烂嫖的水准照样获得无法形容的丰厚待遇，就算它们年年巨额亏损，以至于曾经代表高尚体面的外资白领职业都黯然失色，现在谁还好意思以外资白领职业为荣呢？丢人哦。　　<br />
<br />
　　　　　　<br />
<br />
其他国家也有中国这两种国有经济形式，一种是政府认为需要国有的如核工业之类的企业，一种是供水供电之类的服务企业。但是这类国有经济是被压缩到最小范围内，更要接受公众的严格监控，总之，不能因为你的特殊而骑到民众头上去，而中国的这些公有经济实体却是实实在在地骑到民众头上作威作福了。　　<br />
<br />
　　　　　　<br />
<br />
公有经济实体为何能够骑到民众头上作威作福？难道政府故意与人民作对吗？当然不是，是因为公有经济实际上已经完全成为官僚权力集团的私家后院，只不过挂着公有名义的羊头而已，如果说普通老百姓做公务员的机会已经很小，那么你想进入待遇极其丰厚工作极其轻松的垄断公有经济实体成为一个“正式”的员工则是更难（请注意“正式”这个词，下文将会解释），因为这个公有实体已经基本上对老百姓关上大门。　　<br />
<br />
　 　<br />
<br />
　 　<br />
<br />
中国的国有企业的真实面目是拥有国有企业特殊权力的私有企业，通常称为官僚资本，我们的还加上有中国特色的权利，有了这样权利的官僚资本经济实体对于其拥有者来说那还不是生金蛋的母鸡吗？　　<br />
<br />
　　　　　　<br />
<br />
当今国有经济实体还有一个恶劣的原罪，那就是残酷剥削“合同工”，如果你能做一个光荣的国企正式员工，那你发达了，你可以躺在那里享受惊人的丰厚待遇，如果你只是公有经济实体里的合同工，那么你只是一头干活的驴子，从电信烟草到水电气交通等等各类公有企业都招募了巨量的合同制工人，给予其和正式工相比极其低廉的薪水和待遇，可以说，国有垄断企业之所以有那么高的利润，也和雇佣了很多只需要给予微薄待遇的合同工有很大的关系，如果没有合同工，水电气交通等政府经营实体简直难以想象还要亏损到什么程度，是国有经济实体中合同工的汗水把正式工的钱包浇灌的无比丰厚，真是同处一片天地却是冰火两重天，这是令人发指的残酷剥削，是完全违反人类基本道德准则的行为。　　<br />
<br />
　　　　　　<br />
<br />
私有制的蓬勃发展极大的冲击了这个社会的一潭死水，然而对于这种极具中国特色的私有经济是要打个大大的问号的，因为中国这种私有制有别于通常的私有制，它的主要形式仍然是官僚资本，由特权阶层控制的私有经济占据了私有区域的最主要部分。　　<br />
<br />
　　　　　　<br />
<br />
特权阶层通过掌握国家权力而获取自己经营上的便利，如每一个国有经济实体的背后都有很多特权阶层拥有的私有企业为其“配套”服务，这种“服务”的利润自然是很高的，等于是转移国家资产，掌握国家权利的阶层将政府工程项目交给自己阶层经营的实体承做，这块区域是今天中国暴发户产生的主要源泉之一。而各个政府衙门利用自己区域范围的管理权力垄断了这些区域的产业经营权，不容民众染指或者极力打压普通民众的正当经营，至于那些“纯粹”的草根私有经济实体则时刻面临政府部门的“法规”威胁，环境保护，税收，劳动保障，不规范的法律条款随时可以要你的命，迫使你乖乖的卷缩在特权阶层的淫威下苟且生存。　　<br />
<br />
　　　　　　<br />
<br />
　 　<br />
<br />
外资尤其是西方资本的进入可以说极大的推动了中国的进步，但是对中国的负面作用也越来越明显，一是环境污染的转移，二是西方面对利益的诱惑丧失了他们的道德标准而纵容黑暗的肆虐。我这里就有一个很大的外资项目-----扬子―巴斯夫化工项目，它的污染是显而易见的，配套企业的污染更是惊人，但是扬巴这个项目如果建在德国恐怕就不是这个样子了，今天中国的经济市场对西方确实很有诱惑力，甚至可以用来要挟西方，资本家的根本目标就是利润，这种诱惑迫使西方经常放弃他们的道德和价值观。　　<br />
<br />
　　　　　　<br />
<br />
这里提一下中国一种特有的经济形式，就是政府建筑里的楼台馆所，大家应该注意到，在中国，屁大的政府机构都要建个大楼，虽然屁大的官员都拥有面积超大装修超豪华的办公场所，但是在如此情况下各个政府大楼仍然有巨量多余的空间，这个空间自然就用来经营了，或出租或自营，大家不要小看这个区域范围内的经济力量。而那些数量极其惊人，由政府部门，社会事业部门及军警等所有的商业建筑，恐怕是政府利益集团的很大一笔“非法收入”。　　<br />
<br />
　 　<br />
<br />
　 　<br />
<br />
第三节 土地是特权阶层的最主要掠财源泉<br />
<br />
　　　　　　<br />
<br />
我认为，二十世纪末本世纪初影响中国最大的事件恐怕就是波澜壮阔的圈地运动了，这不仅仅是中国的历史大事，也是世界性的历史事件，我想未来的人类历史学将会给它一个应有的地位的，对于这场运动对中国的影响，可以说真是“十年之间，家不见家，国不见国”。　　<br />
<br />
　　　　　　<br />
<br />
圈地运动的目的何在？你可以说是经济发展的需要，也可以说是敛财的需要，我认为，在圈地运动的初期，因为中国自身环境的局限性，多数国人还没有看出土地的巨大财富价值，那个时候圈地运动的主要目的可能还是为了官员政绩和发展经济的需要，然而就如股票的疯狂一样，当政府（准确的说是特权阶层）发现土地的巨大价值后，圈地运动就变得极其残酷了，从这以后，我认为圈地的唯一主要目的就是掠夺财富，资本家为了百分之三百的利润不惜走上绞刑台，而这场圈地运动的利润早已不是百分之三百的事了，中国特权阶层还拥有资本家所不具备的掌握国家机器的权力，所以，当今的圈地掠夺是一场不可能被阻止的运动。　　<br />
<br />
　　　　　　<br />
<br />
圈地而获取的财富是今天中国暴富集团获取财富的最大源泉，将来仍然是特权阶层敛财的主要方式之一，从城市到农村，从沿海到内陆，谁掌握了土地，意味着谁就有取之不尽的财富，即使是今天的乡级首长，即使是一个小小的村长也常常可以敛财百万千万甚至上亿，原因就是土地的使用权可以任由一个村长书记来决定，由他任意使用，为了这个目的一个村长往往可以通过土地售卖分赃集团调动可怕的国家力量来实现他的目标，这也是乡村变乱的一个主要因素。　　<br />
<br />
第四节：知识不能改变命运：平民失去翻身的机会<br />
<br />
　　　　　　<br />
<br />
直到现在，和家乡村里老人聊天时他们常说：还是你们那时读书好，有分配哦。其实我读书时整个教育环境已经是末日狂花了，只不过我这等草民总算能够安家糊口，还算对得起父母养育之恩。我绝对不赞成“有分配”之说，但现在的农村确实又回到“读书无用”的时代了。　　<br />
<br />
　　　　　　<br />
<br />
上世纪八十年代，也是我读小学中学的时候，那时有过“读书无用”的争论，不过那仅仅是针对一小部分群体而言的，比如对那时有城镇户口的孩子来说，读书还真的无用，反正总有工作，你大学毕业后分配的工作往往还不如高中毕业直接分配的工作待遇好，如一些大型国企职工子弟等等。　　<br />
<br />
　　　　　　<br />
<br />
而对我们这些农村孩子而言，读书几乎是唯一改变命运的道路，那时从小学到大学的学费都不高，就我这里来说，除了极少数非常困难的家庭之外，都是可以供得起孩子读书的，那时中小学的学生流失也比较严重，原因多为他们学习成绩太差，实在不愿意再读。那时的教育总体环境也确实很差，我读小学时教室里一到下雨天都可以养鱼，但是对农村孩子来说，只要你想读书，愿意读书，还是会有一个美好的未来的。　　<br />
<br />
　　　　　　<br />
<br />
现在呢？首先要承认现在社会发财致富的机会应该说是大大的增加了，然而对于任何国家和社会来说，发财总是少数人的机会，创业更是少数人的成功之路，比尔. 盖兹的成功并不能代表美国其他青年没有把握机会。如果一个国家的民众对教育投资结果的感觉普遍是负数的话，那么这个社会必定有大问题。而对于中国的平民来说，对教育投资的普遍感觉早已经是负数了。　　<br />
<br />
　　　　　　<br />
<br />
现在的读书成本究竟有多高早已有无数的计算版本，我给个统计报告，中国教育在线 <a href="http://www.eol.cn/html/06huafei/" target="_blank">http://www.eol.cn/html/06huafei/</a> 的统计，大家自己看。中国青少年研究中心发布报告显示：中国30年来高等教育学费上涨了25倍。可以说，上个学校没有几万元你甭想拿到毕业证，对于年收入一两千元的农民来说，这是几十年拼命劳苦才能够积攒到的数字。　　<br />
<br />
　　　　　　<br />
<br />
这仅仅是大学的花费，小学中学的花费还没算，可以肯定数字也不小，我的印象中没有人认真统计这方面的数据，原因可能是各地差别太大吧，不好统计，但是有一点，既然现在教师的待遇有了很大提高，而国家并没有对教育投入多少，羊毛总是出在羊身上，不管你是城市还是农村，一概不能免俗，我这里就读县中的高中班，几年前的价格就是差一分一万块，还要有关系才行，有人会说：干嘛花钱读？县中不额外收钱的高中班就那三四个，教育是要好的环境的，不然大家为何总是挤破头读重点中小学？现在教师正常课程不愿教，热衷于补课，你敢不补吗？要你掏钱给孩子进课外班，你敢不出这血吗？农村穷苦，可农村教师也是人，三头两头叫学生交点费用，每次不多，细水长流，名目五花八门，真是难为我们的人类灵魂工程师能想得出来那么多花样。　　<br />
<br />
　　　　　　<br />
<br />
在中国什么职业好？公务员，事业单位，大型垄断国有企业，水电气等等特殊国有企业单位，我承认，公务员是全民招聘的，事业单位等等职位也是招聘的，先统考再面试，但地球人都知道这里面的潜规则，我有不少同事也进入政府机关，其他同事为何不去考？凡去考试的人之前都找好关系了，这才去考试去面试，你要是连考试关都不过那就不好说你了，考试可能是真，面试就是做个样子。自然，我不能说平民就进不了政府机关。　　<br />
<br />
　 　<br />
<br />
　 　<br />
<br />
这个社会发展的趋势就是儿子的就业能力要靠老子掌握的权力来决定。今天的中国除了公务员事业单位某些大型国企等少数职业可以给你好的福利待遇之外，还有多少职业能够给予？不要太抱怨私有企业，一些私有企业确实是够黑的，但是就总体情况来说，他们要是按“规矩”办事的话，它们早就死完了。　　<br />
<br />
　　　　　　<br />
<br />
可是公务员这样的优厚职业现在基本上是给利益集团垄断了，官员世袭化，公务员世袭化。民盟青海省委调研课题组完成的对青海省海东地区的平安、乐都、民和、化隆、循化、互助等6县的农村大学生状况调研显示：从2000年到2005年的5年间，回到海东地区的农村大学毕业生共计8863人，截至2005年6 月，尚有5900人待业。其中，乐都县、平安县的待业率分别高达82．8％和96．2％。　　<br />
<br />
　　　　　　<br />
<br />
现在大学生就业薪水竟然低到几百元的地步了，我估计，薪水低到如此地步的职业恐怕也不可能给你各种国家规定的劳保福利了，我这里农村建筑工人中，木工瓦工这样的人一天工资是一百元，加班另算，还供三餐及住宿，而我们象牙塔里的黄金一代竟然比提泥桶的工人还凄惨，然而现实就是这样残酷，一方面，中国获取国家权利的部门及企业的员工薪水之高令人乍舌，我可以断定，今天中国相当一部分权利部门的员工生活水准已经达到或超过港台韩国这些新兴发达国家和地区的生活水平了。而另一方面，中国又是一个廉价劳动力极其丰富的国家，劳动力便宜得跟奴隶一样，因此而获得众多的外国投资，我们的政府并且以此为荣，以中国大学生十几年寒窗苦毕业后却只能获取几百元的薪水为荣，与之对应的是，近年来南方广东地区出现了所谓的“民工荒”。　　<br />
<br />
　 　<br />
<br />
　 　<br />
<br />
我不得不佩服某些中国人的无耻程度，在这个国家里有数亿人迫切需要一个工作来养家糊口时，而我们的喉舌竟然说用工紧缺，要求民工不要过分挑剔，而就在如此低廉的劳动力的情况下，仍然有层出不穷的奴隶黑窑工事件出现，真是让人惊叹。　　<br />
<br />
　　　　　　<br />
<br />
读个十几年书能把自己的家庭搞得倾家荡产，那可真是到了卖儿卖女的地步，穷苦人家的孩子在学校甚至靠检垃圾桶里的食品生活，如果这样的情况下还是毕业即失业，毕业还不能够养活自己，那么现在不时发生在大学生中的悲剧就不难理解了。　　<br />
<br />
　　　　　　<br />
<br />
贫困并不可怕，任何国家任何社会都有贫困群体，可怕的是将平民翻身的机会彻底堵死，而现在的现实就是普通老百姓的晋升机会已经基本被堵死，贫民永远是贫民，官员永远是官员。　　<br />
<br />
　　　　　　<br />
<br />
民众不信神，民众不相信因果报应，民众没有希望没有未来，那么民众还能干什么？　　<br />
<br />
　 　<br />
<br />
　 　<br />
<br />
第五节 还有多少财富属于人民？<br />
<br />
　　　　　　<br />
<br />
国务院研究室、中央党校研究室、中国社会科学院等部门的报告表明：在金融、外贸、国土开发、大型工程、证券五大领域中担任主要职务的，有百分之八十五至九十是高干子女，实际上已形成了官僚资产阶级。至2006年3月底，私人拥有财产（不包括在境外、外国的财产）超过五千万以上的有27310人，超过一亿元以上的有3220人。超过一亿元以上者，有2932人是高干子女，他们拥有资产20450亿元。　　<br />
<br />
　　　　　　<br />
<br />
几年前就有正式披露：中国大陆银行体系存款中的百分之八十，集中由百分之二十的存户持有。我认为这样笼统的统计是不符合中国的国情的。请看国家统计的数据：2004年年末全年全国农村居民人均纯收入2936元，按年人均纯收入低于668元的标准，年末农村绝对贫困人口为2610万人，按年人均纯收入 669-924元的标准，年末农村低收入人口为4977万人。而2005年中国社会科学院分析，如果按照低收入现行标准，即人均年收入低于882元(仅相当于全国农民平均收入水平的1/3)，目前还有8517万人口也属贫困人口。如果按照世界上公认的人均1天1美元以下就属贫困的标准，我国目前还有2.1 亿贫困人口。　　<br />
<br />
　　　　　　<br />
<br />
什么叫没有解决温饱？就是还没有解决吃饱穿暖的问题，通俗地说就是肚子保持饿着，身体保持冻着，我为何这么废话？因为对中国很多人来说根本没法想象这种事情，很多整天饕餮者认为野菜窝头好吃，他们估计会认为那些需要靠野菜窝头来糊口的人真是太有口福了。　　<br />
<br />
　　　　　　<br />
<br />
那些一年收入不到九百元的八千多万农民，我可以担保，他们的财富基本为零，在这个国家里他们的财富完全可以忽略不计，两亿多每天收入在一美圆以下的人，他们拥有的财富也是极其微薄的，中国有九亿农民，其中也有很多人生活富裕，但是大多数人的状况可以用三个字表达：还活着。也就是勉强使自己的生活运转着，为了这种“运转”不得不背井离乡四处打工或者在自己那点土地上拼死拼活的刨食，这里面有多少艰辛和血泪已经不需要再重复，城市里的贫民也许还有可以安慰之处，他们或多或少还可以得到一定的国家救济和照顾，这个群体的数量也是很大的，一般的估计在三千万左右。我的家乡属于中国中上等水平的农村地区，我这里的农民生活还算过得去，楼房逐渐普及，吃点鱼肉算是平常事，但孩子缀学厉害，主要是因为学费太高，很多家庭觉得供不起读书更划不来，农民如果生大病多数去医院治疗几天之后一般也就出院了，撑一天算一天。　　<br />
<br />
　　　　　　<br />
<br />
从以上分析我可以认为：中国近十四亿人口中有一亿人属于被遗忘的群体，这个世界与他们无关，有一亿人基本不存在拥有财富问题，也就是说“财富”这个概念不适用于他们，还有至少四亿人不存在“购买”能力，他们仅仅能保证糊口，商品生产者完全不必考虑他们的消费能力，有一定购买力的是其余近八亿人口。那么我对中国阶层的分类是这样的：第一个阶层（也是处于最顶端的王者阶层）是由几百个家族组成，他们拥有惊人的财富，是这个国家的掌控者。在他们之下是第二个阶层 ――地方性的豪族，数量也许是上万或几万家，这些人控制着地方的权力，自然也拥有无与伦比的财产，第三个阶层是由公务员，事业单位人员、国企的管理人员、垄断国企人员和私营企业主等这些人中的佼佼者以及高级白领阶层等这些群体中的人员组成，第四个阶层是生活比较富裕的一般民众，他们经济上还比较宽裕，但是社会地位不高，对社会没有什么影响力。第五个阶层是由城市平民和农村中生活比较好的农民组成。第六个阶层是贫困群体，也就是四亿没有购买能力的民众。第七个阶层是一亿没有财富的赤贫阶层，第八个阶层就是最后那一亿灾难性赤贫的阶层。　　<br />
<br />
　　　　　　<br />
<br />
那么我可以认为，中国有六亿人拥有的财富微乎其微，几亿平民阶层拥有的财富肯定不及第一第二个阶层所拥有财富的总和多，中国的主要财富掌握在前三个阶层的手里。　　<br />
<br />
　　<br />
<br />
第六节 污染和妓女是中国的代名词<br />
<br />
　　　　<br />
<br />
十来年二十年前，污染这个词对于绝大多数中国人来说都是陌生的，而现在，它成了中国人最熟悉最流行的词语，可见二十年间江山变化之大。　　<br />
<br />
　　　　<br />
<br />
城里的同胞可能不了解，现在农村中的癌症村非常多，这些村里的中青年人不是偶尔死一个，而是成批的死亡。为什么？是因为工业转移，城市要保护环境，污染企业都转移到农村去了，本来农村那些小化工小染织等等已经是超级污染源，所以，现在的农村污染比城市还要恐怖得多，我一直想不透，搞污染企业转移，转移到其他地方就行了吗？难道中国九百六十万平方公里土地中有一半不属于中国人的？　　<br />
<br />
　　　　<br />
<br />
一个国家，几乎所有的河流都被污染，所有的土地都被污染，所有的空气都被污染，这真是空前绝后的壮举。　　<br />
<br />
　　　　<br />
<br />
台湾早在几十年前就得出结论：工业只能在限定的范围内发展，而且工业必须分类布局以便于污染的治理。而现在的大陆仍然是工业遍地开花，就是神仙也没有办法去治理中国的污染问题。　　<br />
<br />
　　　　<br />
<br />
为什么会出现如此情况？难道我们的官员都是二百五吗？原因在于我们的体制要求官员都要去搞GDP，体制要求大家拼命增加税收。有税收才有钱花，有GDP才有官做，在这种情况下，问题永远不可能得到缓解，我们的国土注定成为死亡地带。　　<br />
<br />
　　　　<br />
<br />
中国究竟有多少妓女？不好确定，也不许统计，古时妓女只有较大型城市才存在，后来中小城市也有了，但是“据点”范围很受限制。现在的中国，城市里哪条街道没有妓女营业？别说大中小城市，就是镇子上哪个没有妓女存在的？别说市镇，有些地方连村里都开始普及妓女了。再偏远的地区，国道两旁都是妓馆连立，“妓” 满天下。咱们的农村，水电气化没实现，倒是首先实现妓女化了，咱们的乡镇领导干部天天“闻妓起舞”，“妓”中报国，精尽人亡。农民兄弟有福啊，这比取消皇粮国税幸福多了，真是身在福中不知福。　　<br />
<br />
　　　　　　　　<br />
<br />
妓女是分多种的，那些高尚的、上层社会的妓女咱不谈，人家都代表咱们了。我们只谈中下层妓女，除了纯粹靠卖为生的妓女，还有无数兼职型妓女，在沿海“发达 ”地区，白天工厂里打工，晚上“做”的打工妹不计其数，此为“工妓”，价格低到过三四十元（也就是现在一公斤猪肉的价格），市场不好，供过于求啊，生存实在是不容易，可以说，现在农村出去打工的女孩，相当相当一部分都是以妓为业的，或全职，或兼职。大学每到周末，校园停满了小汽车，都是接美女学生为官僚资本家们服务的，数量更多的女学生则纵横于色场中，此为“学妓”，其数目无法统计。至于兼职卖的小女白领数量相对比较有限，此为“白鸡”。街头那些属于中老年享受型的“摸妓”等等就算了，不纳入妓女范围，否则范围太大，有“反妓扩大化”之嫌。　　<br />
<br />
　　　　<br />
<br />
中国妓女之普及，这是一个空前绝后的成绩，其数量也许是大几百万，也许是一千来万，甚至有人认为中国的妓女数量有数千万，这个数字感觉太过了，但是有一点可以肯定：全世界妓女数量的总和也根本无法与中国的妓女数量相比。　　<br />
<br />
中国妓女数量占了世界的绝对多数。很难说这是一个值得炫耀的事情。　　</div>

							
							
							
							
