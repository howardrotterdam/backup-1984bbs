此外，金正日访华期间乘坐的车辆为超级豪华车奔驰迈巴赫，市价达5000万元人民币。中方还为大规模朝鲜代表团提供了4 0 多辆轿车和巴士。而这些费用，即金正日一行在华期间的所有费用，全部由中方负担。<br />
<br />
然而，形成鲜明对比的是，媒体报道德国总理默克尔在中国南京访问时的一些细节：默克尔抵达南京后，获安排入住南京市“索菲特银河大酒店”顶楼的总统套房，套房400多平方米、可以看到南京全景。但默克尔认为这个安排过于奢华，坚持要入住70多平方米的普通商务客房，房价1800元（总统套房的二十分之一）。默克尔说：“普通豪华套间条件已经足够好了。”早餐时的细节更令人感动。酒店本来准备了两套方案，房内用餐或是在只为贵宾服务的46楼行政楼层索菲特会所用餐，但默克尔总理坚持和一般住店客人一样去7楼锦绣西餐厅，而且不进VIP包间，和随行的德国工作人员一道在大厅吃自助早餐。吃早饭时，默克尔谢绝了工作人员的服务，坚持自己到自助餐台取食物，并自己动手切法式长棍面包。默克尔在拿食物时，一不小心把麦片面包掉在地上，她不让赶过来的餐饮经理帮忙，而是自己将面包捡起，放回了自己的盘中。默克尔的早餐很简单，煎鸡蛋卷、奶酪饼、西瓜、也包括掉到地上的麦片面包……。<br />
<br />
一个贫困的独裁专制的国家，人民还在忍饥捱饿，其领袖会如此穷奢极欲；而一个富裕发达的民主国家，其领导人会如此简朴节省。差别何以如此之大？究其根源，决不只是当事者个人的品质和修养所决定的 。根本原因恐怕是当权者获取权力的方式截然不同。<br />
<br />
和所有成熟的民主国家的领导人一样，默克尔的权力是选民赋予的，她在行使权力时要受民众的监督，要受到诸多的限制和约束。默克尔在总理位置上表现的简朴节省，可以说是德国人民长期监督的结果。<br />
<br />
据说，金正日最近带着要继位的儿子来朝拜了当年金日成学习，战斗过的地方。天天感受革命传统教育的中国人民一看就明白，这是金家在向世人宣示：江山是我金家打下的，金家子孙才是最合法的权力继承人。这和我国某些领导主张只有“太子”掌权才能保证“祖坟”不被刨有异曲同工之妙。说这种权力是人民赋予的，那纯粹是强奸民意，，这种谎言之所以能支撑下去，完全依赖暴力专制。有这样的权力背景，金正日的穷奢极欲也就不难理解了。在中国有相似的权力背景，当然就会有前赴后继的贪官污吏和每年几千亿的三公消费；当然就会有不惧“屁民”的书记；当然就会有责问记者是替党说话还是替百姓说话的局长；当然就会有因为记者的提问不合味口而变脸，进而夺走记者录音笔的“能听进逆耳之言的有雅量的”省长……<br />
<br />
最近，习近平在讲话中强调了权力观的首要问题是“权力是谁赋予的、权力该为谁服务”。而“权力是谁赋予的”是根本，谁赋予掌权者权力，掌权者就当然为谁服务。这个问题直接涉及到我国的体制改革。面对庞大的既得利益集团，如何把一级级的“领导”赋权变成一级级的人民赋权，这是我们的社会走向长治久安必须解决好的课题。</div>

							
							
							
							
