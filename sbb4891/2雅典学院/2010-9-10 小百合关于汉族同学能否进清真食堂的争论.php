下面是一个少数民族老师的回复：<br />
-----------------------------------------------------<br />
发信人: longin (Communication and Understanding), 信区: V_Suggestions<br />
标&nbsp;&nbsp;题: 【回复】：清真餐厅 汉人不可以去吗！！<br />
发信站: 南京大学小百合站 (Fri Sep 10 09:21:15 2010)<br />
<br />
<br />
<br />
作为校区的一名工作人员，同时也作为一名少数民族教师，我来做个回复吧。<br />
<br />
首先，必须说明的是，学校是培养人的地方，学校提供的任何教学和服务设施对于任何同<br />
学都是平等和开放的。因此，也就是说，汉族同学进入清真餐厅用餐这一行为没有任何人<br />
可以禁止。<br />
<br />
其次，此贴说明的另一个问题是“人文关怀”的问题，也就是尊重他人，尊重自己。每个<br />
同学因为来自不同区域，不同家庭都有不同的习惯。我想大学除了教给我们知识和文化以<br />
外，更重要的就是通过在与不同区域、不同家庭的同学接触的过程中，了解他人的习惯，<br />
尊重他人的习惯，学会求同存异，或者说和而不同的相处下去。少数民族同学除了带有本<br />
地区的习惯外，也带有其民族习惯。尤其是维吾尔族、哈萨克族、回族等同学，他们的生<br />
活习惯、饮食习惯和汉族同学有很大的差异。比如不吃猪肉，在他们心中（没有其他的意<br />
思，只是解释一下），猪是不洁之物，这在这些少数民族同学心中，已经是根深蒂固的观<br />
念，已经内化为一种习惯。所以这是我们汉族同学应该了解和尊重他们的地方。此外，少<br />
数民族同学也应该了解汉族同学的习惯，也应该善意、主动沟通和解释自己民族的习惯和<br />
想法。我想在南京大学，平等而温和的沟通应该可以解决绝大多数的问题和矛盾。<br />
<br />
第三，我想说说目前清真食堂的现状。目前仙林校区清真食堂主要是给200余位少数民族同<br />
学提供餐食。其实，面对高峰时期大食堂拥挤的状况，从后勤部门来说，可以供应更多的<br />
菜肴给汉族同学，缓解就餐压力。但是，面临两个问题。一个是餐具混用问题，这是一个<br />
民族习惯问题。还有一个就是菜肴的准备量问题。准备多了，没有人消费，造成浪费，准<br />
备少了，汉族同学先来，先吃完了，少数民族同学唯一的用餐食堂没有伙食供应了。由于<br />
用餐的随机性和饮食结构的差异性，很难对每顿餐食的量进行动态调整，因此，“我们本<br />
来不够吃”就成为一个理由。<br />
<br />
最后，我想说，这个问题大家都退一步想想就很简单了。汉族同学想要品尝清真风味，来<br />
清真食堂无可厚非，如果餐食量允许的的话，并且自带餐具，少数民族同学应该热情欢迎<br />
同学来品尝。同时，汉族同学也尽量考虑一下餐厅人流量和菜肴储备，尽量不要“一哄而<br />
上”。歧视是不存在的，所谓的“挑衅”也是理解上的差异，退一步海阔天空，相信大家<br />
都是好同学，好朋友！<br />
