提到：<br />
<br />
（仙林校区）<br />
昨天，我和舍友下课后，大概12点15分到的食堂。<br />
因为室友们之前去过一次清真餐厅，觉得那里的饭挺好吃的，所以他们想带我去吃。<br />
我们来到了六食西南角的清真餐厅，正在看着小黑板，决定要选什么吃。<br />
&nbsp; &nbsp; 这时，排在前面的一个回族（或其他族）男学生回过头，看着我们，然后问：<br />
 “你们是回族的吗？”男生还没有什么表情，我们也不知道他为什么这么问。<br />
&nbsp;&nbsp;我们回答“不是的。”<br />
 “这里是清真餐厅。”男生有些不满的感觉。<br />
&nbsp;&nbsp;“我知道。”我又补充了句：“我们就是来这里吃饭的。”<br />
&nbsp;&nbsp;紧接着，男生有些面露怒色，很挑衅地说了句“我们本来就不够吃了，懂了吧。”<br />
&nbsp;&nbsp;我们四个女生立刻感觉到不好意思和有些害怕，然后走了。<br />
<br />
&nbsp;&nbsp;走出餐厅，我立刻觉得不对，为什么我们要不好意思！？<br />
&nbsp;&nbsp;什么叫“我们本来就不够吃了”？！<br />
&nbsp;&nbsp;最后对我们四个女生吐出的“懂了吧”又是多么的挑衅和不礼貌！<br />
 <br />
&nbsp; &nbsp; 我很不开心，虽然以前听同学说过有些少数民族的同学很排外，但始终觉得中华民族<br />
的人民不都是一个大家庭吗，而回想起昨天的事情来，我真的有些担忧和害怕。<br />
&nbsp; &nbsp; 如果当时我没有愣住然后离开，而是坚持在那里吃饭，不知道那个男生的怒色又会演<br />
变成什么东西来回击我们，是否今天百合的十大上就会有“餐厅门”了。。。<br />
<br />
&nbsp; &nbsp; 今天，当我再和我的舍友说要去清真餐厅吃时，他们都不敢了（害怕再遇“牛”人）<br />
。。。<br />
&nbsp; &nbsp; 从另一个方面考虑，他们吃不惯我们吃的食物（当然也有不少少数民族同学在外面的<br />
四，五，六食吃饭），如果我们去那里吃，那么他们显然会供不应求，但是这就需要用那<br />
种语气来要求我们走吗！<br />
&nbsp; &nbsp; 舍友之后生气又无奈地说“要是我们排外，他们还不知道到哪里去了呢！”然而，这<br />
句话终归是安慰自己的气话，因为我们谁也没有胆量再去那里了，但是我始终觉得不应该<br />
这样了事，应该让更多的人知道这种现象，让更多的人来透过现象表达真实想法，可能我<br />
是有些偏激（我始终相信自己是比较有邹世敏的个性的），那就让同学们都来看看，然后<br />
给个评判吧！<br />
