													<h2>这个我觉得和思想保守与活跃无关</h2>
						
						
																											<div id="postmessage_439377" class="t_msgfont">这里主要是政治论坛，以八卦、乱扯、掐架为主。搞纯学术的话有点深奥。呵呵<br />
<br />
转一篇文章请楼主过目过目：<br />
<br />
英语最难的地方就在于绝大多数的单词是原生词，而汉语大多是组合词。举个例子，goblet-高脚杯，汉语用了最简单的三个字“高--脚--杯”，既形象又明了的表述了这个事物的基本特点和外貌特征，这个词不需要特意去学习就可以马上理解，既所谓的无师自通，而且只要它在眼前过一遍就永远不会忘记，因为它太简单太形象了。反过来我们来看看英文是如何表达的，“goblet”，从字面上看不出任何意义，也完全猜不出这个字为什么要这么创造，它更像是一组无序字面组合，这个字母组合代表了一个某种酒杯意义。有人曾经说过，英语是密码类语言，在第一次遇到这个词时，是无论如何猜不出它的意思的，就算查字典知道了它的意思，也很难保障下次再遇到时候还能记得它的意思。（虽然台湾某学者编撰了教人猜字根的手册，但本人对其持怀疑态度并没有将其读完。）除了“goblet-高脚杯”之外，我们可以在总体的90%的字词构成方式上找到相同的例子，例如，gladiator-角斗士，hibernation-冬眠等等。英文只有5%左右的单字是组合词，比如，backwater-回水（由 back和water组成），blowhard-吹牛大王（由blow和hard组成）。记忆组合词最大的好处就是：几乎不需要记忆。<br />
<br />
汉语体系是由字，词，短语，句子构成的，常用的字大概在1700个左右。而英语是由字，短语，句子构成，它并不存在词的概念，所谓的单词，严格上讲，就是英语的字，常用的英文单字大概在15000左右。除了字数的悬殊差距之外，我们可以再从字的学习难度上对比一下。汉字是象形文字，重视形、音、意三者的集合，我们在平日的阅读中经常遇到不认识的字，这时我们往往通过它的形状来猜测它的意义，而形状即是图像信息，是很容易被人类大脑记住的信息类型，举个简单的例子来说明下图形的威力，我们在碰到老熟人的时候往往一眼就能认出他的脸，却怎么也想不起他的名字来。其实每个汉字都如同一张性格鲜明的脸庞，这是象形文字的本质，各位可以对比一下自己少年的时候同时记忆汉字和英文单词的费力程度，从而得到一个更直观的答案。（英语也有“导图式单词学习法”，即给每个单词配一副图画以帮助记忆，但大多数只在儿童教育领域使用，且单词总数十分有限。）我相信，很多人都有背单词背了几十遍仍旧记不住的痛苦经历，其实美国人在学习英语的偏词、难词的时候并不比中国人更容易，他们只是没有选择的余地而已。就整体而言，中国人在学汉语的时候用的成本要比美国人学英语低的多。这个世界上只有一个国家的人可以在小学勉强及格的情况下读完《鹿鼎记》，中学勉强及格的情况下就读完四大名著，只有中国的汉语有这种伟大的具形性表达方式和高度的可猜测性。在英美国家只要有能力完整流畅的读完莎士比亚就算文化人，在中国，人们对文化人的定义标准显然要高的多。<br />
<br />
<br />
多年以前东西方的人们就达成了一个共识：汉语是最难学的，而英语要简单的多。事实上，本人认为情况恰恰相反。除了构成方式的先天不足之外，现代英语在传承古代英语的方面也比现代汉语完整的多。现代汉语在抛弃了诸多如“翘楚”之类传统用词并将繁体字变为简体字之后，已经成为了目前世界上最简单最科学也是最白话的语言体系，现有词汇绝大多数采用的表达方式是简单字的变化组合，即便再造新词也是通过以现有常用字对事物的某种特性的表述来完成，而英语每年都会产生大量的原生新字。从这个方面来讲，汉语是组合的艺术，它是内敛的。英语是创造的艺术，它是发散的。英语发展至今单字已近百万，并且将会无可避免的永远膨胀下去。<br />
<br />
这就是英语跟汉语本质的区别。</div>

							
							
							
							
