<br />
我博士要做的就是“中文自动分词” （Chinese word segmentation），也就是用电脑自动给中文插入空格，变成类似英文的那种一个一个词的形式，这样才能接下来做词性分析，句法分析，语义分析等等。分词是必须要做的一步。<br />
<br />
分词也会有很多歧义，比如“一次性交500元”、“杭州市长春药店”等等，用计算机也不是很容易做。这个其实也是汉语比较有意思的一点吧。<br />
<br />
目前的计算机在一些比较受限的数据中，分词正确率能到95%到97%的样子。<br />
<br />
而且与英语不同，不但大众，就算是语言学家对中文分词的标准都莫衷一是。比如“猪肉”、“白菜”，分别是一个词还是两个词。<br />
<br />
空格跟简繁一样，只要大众习惯了，再改就难了。<br />
<br />
拼音还有个不现实的地方是，用拼音之后，我们中国之前所谓的古籍，所有用汉字写的古今文献，全部要翻译成拼音，这几乎是不可接受的。<br />
<br />
作为孤立语、分析语的汉语，用汉字也有它自己的道理与优势。<br />
