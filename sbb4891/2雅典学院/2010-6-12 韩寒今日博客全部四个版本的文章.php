													<h2>韩寒：我的道歉</h2>
						
						
																											<div id="postmessage_677589" class="t_msgfont">有个朋友告诉我，参与圣战的有相当多网友，你别得罪这么多年轻人。我说，我又不是投机分子，看哪里人多就替人家说两句话拉点支持者来。我还是这个观点。以前很难想象，在这个没有信仰的国家里，居然还有一场“圣战”。我朋友又说，你刻意回避了我们其实只是要求那群脑残粉丝向武警和警察道歉这个正当要求。那就来说一说这个正当要求。<br />
<br />
 <br />
<br />
“圣战”这个名字起的非常好，虽然我特别反感这个词汇，但它特别容易激起人们的狂热，再加上有爱国主义和抵御外来文化入侵的大伞，有要求那些脑残粉丝向武警战士们道歉的目的，在政治上也显得非常的正确，最后加上了“这些中国粉丝破坏了中国在国际上的形象，破坏了世博会的和谐”这一护身符，一切真是太周全了。就差官方盖个章承认其合法性了。可惜不会的。5月30日，世博园的这些韩国偶像团体粉丝们的确秩序混乱，并且对武警无礼，但这远远不够“破坏中国在国际上的形象”的程度，真正破坏了中国在国际上的形象的事件，相信你们这些网络热血青年们不是不知道，但是你们知道这是敏感的，没有上面的那些护身符，你敢组织一个“圣战”，你早就被跨省了，你我都会懦弱，相关内容和帖子都会被屏蔽，聪明的孩子们马上会察觉这不要去碰，自然也没有那么多人应和。<br />
<br />
 <br />
<br />
我之所以反对圣战，是因为这是一个太容易被扩大的词汇和行动，而且带有强烈排他性，我会被变成脑残，有些媒体马上也将变成脑残，他们执着于整个事件的细节，一旦你没把来龙去脉完全说一遍，你也是不明真相的脑残，想给双方都说几句话的就是不了解那些脑残有多脑残的脑残，最后只要对他们的行动持有异议，你一概是脑残，他们开始感受到一个组织给他们带来的一呼百应的力量，觉得身边都是兄弟。中国历来的悲剧都是因为必须排他而造成的。如果没有人反对它，他有可能由某场演唱会开始形成一次学生运动，而且是一次丢脸的学生运动。你们的第一次团结的力量居然用于修理自己品味不高的小弟弟小妹妹，甚至还拉上了武警给你们做合法靠山，连要帮那些韩国明星的粉丝竖立国格都出来了。我反正有点不好意思。虽然这是一件小事情，说到底就是去爆别人的贴吧而已，但每一方每一个人的心态都那么有代表性。<br />
<br />
 <br />
<br />
虽然不少人也说你们是愤青，打了鸡血，其实我不太赞同，这一次和上一次的家乐福事件还有所不同，可惜一腔热血，撒错地方。<br />
<br />
 <br />
<br />
坦率的讲，中国的警方其实在对待近年的群体事件还算是比较克制的，虽然他们有时候是我们的兄弟，虽然他们有血有肉，但他们是国家机器，他们今天在维系治安，救难救灾，我赞美他们，他们也会滥用职权，掩盖真相，我不敢去批评他们。因为几个心智尚未成熟的孩子向他们吐了口水而发动几十万学生要求他们整个群体道歉，而国家机器曾经所有的失职，我们从来没有听到过任何一个道歉，根本无权要求，甚至没有解释。说到底，小孩子不懂事，只是吐了几口口水，推搡拉扯了几下而已，演唱会的保安都能承担这些，我们的武警战士难道不能承担这些，需要你们来做出煽情视频，扯上玉树地震，播放感人音乐，来证明武警遭受了必须被道歉的委屈？你见过另外一些场景，另外一些弱者，面对国家机器的那种真正的委屈和绝望么？他们享受过道歉么？<br />
<br />
 <br />
<br />
我觉得这一个吐口水的孩子可以向这一个被吐到的战士说对不起，这一个群体不应该向另一个群体道歉，虽然做错就要道歉，但这是第一次群体向群体的道歉。6月9日，一群学生怎么可以逼着另一群学生率先向国家机器隆重道歉。<br />
<br />
 <br />
<br />
圣战没有任何的必要，不是日本文化，韩国文化，欧洲文化，美国文化入侵我们，只是因为中国文化在世界上太过于弱势了，所以你才觉得人家是在入侵。文化应该是互相交流互相容纳最后互相进步的，没有了韩国，一样有别的国家的文化来占领我们年少的小弟弟小妹妹们，一样有操着其他国家语言的人成为他们的偶像，我也不喜欢他们，但至少韩国人没有教他们吸毒，暴力，乱性，互相攻击，阶级斗争，她们也就是显得有些突兀和幼稚，让这些文化自然生，自然灭，我们也都从来没有试过去了解她们，没有那么坏的孩子，也就是在网络上护着自己的偶像，谁不这样，就像圣战中的很多人，没想太多只是发了一个帖子，然后就要想尽一切理由要证明自己的正当性和师出有名。三年前，我企图消灭现代诗和嘲讽现代诗人，我今天觉得很惭愧，当时的我错了，这是我的道歉。不要去人为的妄图消灭任何一种文化和教训它的受众群体，哪怕它不算什么好东西，除非它反人类。把中国自己的文化弄弄好，去入侵别人吧，这才能带来最踏实的民族自豪感。<br />
<br />
<br />
<br />
<br />
修改以后的版本：谢谢我的对手，你们让我学到很多，让我知道长路漫漫。关于类似的一切，我的看法从未改变。两年前我就已经说过一遍，雷同观点如今不想再多说了，说来说去都是一样，说多了就累了，在累之前我认输，否则就灰心了。你们胜利了，请随意。如果你是我的读者，我希望你们不要以任何名义去驱逐任何一种文化，更不要想教训和消灭它的受众群体，无论是文化还是政治都不能排他，也不能代替别人做出选择，哪怕它很傻，哪怕它不合你的口味，只要它不反人类。我曾经无意识的带领你们去往各个博客铲除异己，如今我欣喜的看到我们共同的进步，四年前的我一定带不走今天的你。热血一定要洒在它该洒的地方，否则它就叫鸡血。在此我也正式向现代诗歌以及现代诗人道歉，三年前我的观点是错的，对你们造成的伤害带来的误会，我很愧疚，碍于面子，一直没说，希望你们的原谅与理解。愿文化之间，年代之间，国家之间都能消除成见，为了……你知道的。<br />
我看好阿根廷。</div>

							
							
							
							
