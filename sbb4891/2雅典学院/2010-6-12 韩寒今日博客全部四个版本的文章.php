													<h2>韩寒：我的道歉</h2>
						
						
																											<div id="postmessage_676853" class="t_msgfont">有个朋友告诉我，参与圣战的有相当多网友，你别得罪这么多年轻人。我说，我又不是投机分子，看哪里人多就替人家说两句话拉点支持者来。我还是这个观点。很难想象，在这个没有信仰的国家里，居然还有一场“圣战”。我朋友又说，你刻意回避了我们其实只是要求那群脑残粉丝向武警和警察道歉这个正当要求。那就来说一说这个正当要求。<br />
<br />
 <br />
<br />
“圣战”这个名字起的非常好，虽然我特别反感这个词汇，但它特别容易激起人们的狂热，再加上有爱国主义和抵御外来文化入侵的大伞，有要求那些脑残粉丝向武警战士们道歉的目的，在政治上也显得非常的正确，最后加上了“这些中国粉丝破坏了中国在国际上的形象，破坏了世博会的和谐”这一护身符，一切真是太周全了。就差官方盖个章承认其合法性了。可惜不会的。5月30日，世博园的这些韩国偶像团体粉丝们的确秩序混乱，并且对武警无礼，但这远远不够“破坏中国在国际上的形象”的程度，真正破坏了中国在国际上的形象的事件，相信你们这些网络热血青年们不是不知道，但是你们知道这是敏感的，没有上面的那些护身符，你敢组织一个“圣战”，你早就被跨省了，你我都会懦弱，相关内容和帖子都会被屏蔽，聪明的孩子们马上会察觉这不要去碰，自然也没有那么多人应和。<br />
<br />
 <br />
<br />
我之所以反对圣战，是因为这是一个太容易被扩大的词汇和行动，而且带有强烈排他性，我会被变成脑残，有些媒体马上也将变成脑残，他们执着于整个事件的细节，一旦你没把来龙去脉完全说一遍，你也是不明真相的脑残，想给双方都说几句话的就是不了解那些脑残有多脑残的脑残，最后只要对他们的行动持有异议，你一概是脑残，他们开始感受到一个组织给他们带来的一呼百应的力量，觉得身边都是兄弟。中国历来的悲剧都是因为必须排他而造成的。如果没有人反对它，他有可能由某场演唱会开始形成一次学生运动，而且是一次丢脸的学生运动。他们的第一次团结的力量居然用于修理自己品味不高的小弟弟小妹妹，甚至还拉上了武警给你们做合法靠山，连要帮那些韩国明星的粉丝竖立国格都出来了。我反正有点不好意思。虽然这是一件小事情，说到底就是去爆别人的贴吧而已，一场游戏一场梦，但每一方每一个人的心态都那么有代表性。<br />
<br />
虽然不少人也说你们是愤青，打了鸡血，其实我不太赞同，这一次和上一次的家乐福事件还有所不同，可惜一腔热血，撒错地方。<br />
<br />
 <br />
<br />
