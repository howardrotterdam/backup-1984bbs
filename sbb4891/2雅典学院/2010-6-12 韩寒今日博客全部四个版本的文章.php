&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;谢谢我的对手，你们让我学到很多，让我知道长路漫漫。关于类似的一切，我的看法从未改变。两年前我就已经说过一遍，雷同观点如今不想再多说了，说来说去都是一样，说多了就累了，在累之前我认输，否则就灰心了。你们胜利了，请随意。如果你是我的读者，我希望你们不要以任何名义去驱逐任何一种文化，更不要想教训和消灭它的受众群体，无论是文化还是政治都不能排他，也不能代替别人做出选择，哪怕它很傻，哪怕它不合你的口味，只要它不反人类。我曾经无意识的带领你们去往各个博客铲除异己，如今我欣喜的看到我们共同的进步，四年前的我一定带不走今天的你。热血一定要洒在它该洒的地方，否则它就叫鸡血。在此我也正式向现代诗歌以及现代诗人道歉，三年前我的观点是错的，对你们造成的伤害带来的误会，我很愧疚，碍于面子，一直没说，希望你们的原谅与理解。愿文化之间，年代之间，国家之间都能消除成见，为了……你知道的。<br />
