《反圣战》_ 韩寒<br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; 有个朋友告诉我，参与圣战的有二十多万人，你别得罪这么多年轻人。我说，有两亿人也没用，我又不是投机分子，看哪里人多就替人家说两句话拉点支持者来。我还是这个观点。以前很难想象，在这个没有信仰的国家里，居然有一场“圣战”，目标是另外一个国家的偶像团体和我们自己国家的狂热粉丝。我朋友又说，你刻意回避了我们其实只是要求那群脑残粉丝向武警和JC道歉这个正当要求。我说，哟，你们怎么突然间又变成一家亲了。<br />
<br />
<br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;“圣战”这个名字起的非常好，它特别容易激起人们的狂热，再加上有爱国主义和抵御外来文化入侵的大伞，有要求那些脑残粉丝向军警战士们道歉的目的，在政治上也显得非常的正确，最后加上了“这些中国粉丝破坏了中国在国际上的形象，破坏了世博会的河蟹”这一护身符， 一切真是太周全了。可惜不会的。5月30日，世博园的这些韩国偶像团体粉丝们的确秩序混乱，并且对武警无礼，但这远远不够“破坏中国在国际上的形象”的程度，真正破坏了中国在国际上的形象的事件，相信你们这些网络热血青年们不是不知道，但是你们知道这是危险的，没有上面的那些护身符，你敢组织一个“圣战”，你早就被跨省了，相关内容和帖子都会被屏蔽，聪明的孩子们马上会察觉这是敏感的，自然也没有那么多人应和。<br />
<br />
<br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;我之所以反对圣战，是因为这是一个太容易被扩大的词汇和行动，很明显已经有人开始煽动和挑拨仇恨，有人开始指出那些脑残粉丝们去周恩来的贴吧骂周恩来，接着你是不是打算让他们把所有全国人民有好感的名人都骂一遍？你当人家真的傻成那样？你觉得你很聪明为圣战做出了贡献？如果没有人反对它，他有可能真的形成一次学生运动，而且是一次丢脸的学生运动。多少年来，中国的学生们享受着资讯的半开放，你们其实早就了解这个世界，但是你们的第一次团结的力量居然用于修理自己品味不高的小弟弟小妹妹，甚至还拉上了武警给你们做合法靠山，我觉得很羞愧和遗憾，当热血撒错了地方，它就叫鸡血。<br />
<br />
<br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;坦率的讲，中国的警方其实在对待近年的群体事件还算是比较克制的，虽然他们有时候是我们的兄弟，但他们是国家机器，他们今天在维系治安，救难救灾，我赞美他们，他们也会滥用职权，掩盖真相，我不敢去批评他们。因为几个心智尚未成熟的孩子向他们吐了口水而发动几十万学生要求他们整个群体向国家机器道歉，而国家机器曾经所有的失职，我们从来没有听到过任何一个道歉，根本无权要求，甚至没有解释。我觉得这一个吐口水的孩子可以向这一个被吐到的战士道歉，这一个群体不应该向这另一个群体道歉，虽然做错就要道歉，但这是第一次群体向群体的道歉，6月9日，一群学生怎么可以逼着另一群学生率先向国家机器道歉。<br />
