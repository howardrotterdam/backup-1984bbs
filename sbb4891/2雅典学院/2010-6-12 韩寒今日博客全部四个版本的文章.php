<br />
圣战没有任何的必要，不是日本文化，韩国文化，欧洲文化，美国文化入侵我们，只是因为中国文化在世界上太过于弱势了，所以你才觉得人家是在入侵。文化应该是互相交流互相容纳最后互相进步的，没有了韩国，一样有别的国家的文化来占领我们年少的小弟弟小妹妹们，一样有操着其他国家语言的人成为他们的偶像，我也不喜欢他们，但至少韩国偶像没有教他们吸毒，暴力，乱性，互相攻击，阶级斗争，她们也就是显得有些突兀和幼稚，让这些文化自然生，自然灭，我们也都从来没有试过去了解她们，没有那么坏的孩子，也就是在网络上护着自己的偶像，谁不这样，就像圣战中的很多人没想太多只是发了一个帖子，然后就要想尽一切理由证明自己的正当性和师出有名。三年前，我企图消灭现代诗和嘲讽现代诗人，我今天觉得很惭愧，当时的我错了，这是我的道歉。<br />
