&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;坦率的讲，中国的警方其实在对待近年的群体事件还算是比较克制的，虽然他们有时候是我们的兄弟，虽然他们有血有肉，但他们是国家机器，他们今天在维系治安，救难救灾，我赞美他们，他们也会滥用职权，掩盖真相，我不敢去批评他们。因为几个心智尚未成熟的孩子向他们吐了口水而发动几十万学生要求他们整个群体道歉，而国家机器曾经所有的失职，我们从来没有听到过任何一个道歉，根本无权要求，甚至没有解释。说到底，小孩子不懂事，只是吐了几口口水，推搡拉扯了几下而已，演唱会的保安都能承担这些，我们的武警战士难道不能承担这些，需要你们来做出煽情视频，扯上玉树地震，播放感人音乐，来证明武警遭受了必须被道歉的委屈？你见过另外一些场景，另外一些弱者，面对国家机器的那种真正的委屈和绝望么？他们享受过道歉么？<br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;我觉得这一个吐口水的孩子可以向这一个被吐到的战士说对不起，这一个群体不应该向另一个群体道歉，虽然做错就要道歉，但这是第一次群体向群体的道歉。6月 9日，一群学生怎么可以逼着另一群学生率先向国家机器隆重道歉。<br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;圣战没有任何的必要，不是日本文化，韩国文化，欧洲文化，美国文化入侵我们，只是因为中国文化在世界上太过于弱势了，所以你才觉得人家是在入侵。文化应该是互相交流互相容纳最后互相进步的，没有了韩国，一样有别的国家的文化来占领我们年少的小弟弟小妹妹们，一样有操着其他国家语言的人成为他们的偶像，我也不喜欢他们，但至少韩国人没有教他们吸毒，暴力，乱性，互相攻击，阶级斗争，她们也就是显得有些突兀和幼稚，但我们也都从来没有试过去了解她们，没有那么坏的孩子，也就是在网络上护着自己的偶像，谁不这样，就像圣战中的很多人，没想太多只是发了一个帖子，然后就要想尽一切理由要证明自己的正当性和师出有名。把中国自己的文化弄弄好，去入侵别人吧，这才能带来最踏实的民族自豪感。<br />
