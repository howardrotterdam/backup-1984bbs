													<h2>来一下词语分析吧</h2>
						
						
																											<div id="postmessage_328951" class="t_msgfont">“美帝扶植独裁傀儡的问题”<br />
<br />
“美帝”这个词里蕴含的意思很多，相信读者都有自己的判断，我不多说，关键不在这里<br />
因为“美国扶植的独裁傀儡”之类的表述也很多，关键的在于“扶植”和“独裁”“傀儡”三个核心词<br />
<br />
“扶植”，这个词本身就是贬义词，如果考虑中性色彩一样的词，是“帮助建立”，或者说“协助建设”<br />
一个国家帮助另一个国家，我觉得是无可厚非的事情<br />
世界历史上，某A帮助某B立国，那是很崇高的事情，比较有名的事情，就是法国对美国独立战争的帮助，玻利瓦尔的光荣业绩到现在也被各派提及，即使所谓&quot;左派&quot;，也喜欢格瓦拉，而在这个环境中，还有“输出革命”的光荣口号，哪怕到了坛语境下，“存亡国，继绝祀”也是一个很光荣的事情，齐桓公在大部分情况下，还是正面评价多一些吧<br />
<br />
但使用了“扶植”，本身就有很强烈的价值判断的意思，已经否定了这个行为了，那么，如果按照“扶植”的非中性用法，“苏共”也可以“帮助建立”了中共和朝鲜劳动党，中共扶植了赤柬，这是本身就是一种有罪推定，或者说，预设的否认了这些人价值<br />
<br />
再看傀儡<br />
傀儡是木偶的意思，指代理人，当然，这个词也是贬义的，跟“扶植”有些像，因为“代理人”和“共同利益人”、“同路人“的界线并不清楚，比如，二战后的日本政府算美国”扶植“起来的吧，但要说所有日本政府都是美国的”傀儡“、完全听命美国政府，我觉得好像还是说不过去的，中共由苏共”扶植“，但到底是不是”傀儡“，不仅本站的人未必答应，恐怕毛左们也会”不高兴“的。<br />
<br />
由美国建立的政府，对美国的依赖肯定强一些，这应该是合情合理的吧。所以，”扶植“和”傀儡“本身只是添加了感情色彩，所以变成了一个否定的意思，靠词语辨析就能够清楚搞掂。现在只剩下”独裁“这一个词了。这才是这段语句的关键，因为“独裁”这个词掩盖了最关键的东西。<br />
<br />
仔细说说”独裁“。“独裁”是“一个人专断”的意思，《晋书》里的语源，在汉语里，并没有“政治制度”或者相关类的意思，但当代人使用“独裁”一词，更多的情况下，实际上是使用了希腊-罗马语境及其后学里的“独裁”翻译出来的中文，也就是说，使用的是“dictator”及&quot;相关词&quot;的翻译。<br />
<br />
那dictator是什么那？最早，罗马有独裁，也就是“dictator”大家有兴趣可以去维基的英语版“Roman dictator”看下，“Roman dictator”在罗马共和国建立后不久就有了，（王政结束后9年，501BC），最关键的是，“独裁”由元老院任命，不能传子，独裁也不是常设的职位，只是为了应付一些紧急情况，比如内乱、外敌而建立――几乎所有都独裁，都是有任期的，只有凯撒是终生独裁，结果他被做掉了，被做掉的目的也是为了维护罗马共和国――当然，目的最后还是失败了，那是题外话。所以”dictator“不是完全的贬义，尽管罗马之后，”独裁者“大多是骂人的话。<br />
<br />
之所以花那么大的篇幅讲“独裁”的渊源，我想表述的意思是：“独裁”并不是文明世界的政治中的一个完全不可接受的概念，但在“美帝扶植独裁傀儡”这个表述中，”独裁“掩盖的是独裁背后的”极权社会“。（想知道什么是极权社会和专制社会的区别，以及分不清极权和集权的人，去看看汉娜阿伦特的书吧）<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;从理论上说，独裁可以和极权很接近，甚至夸张一些说，可以认为独裁是极权条件，但独裁绝对不是极权的充分条件。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;最简单一方面，就独裁本性来说，独裁有任期，这对列宁、斯大林、希特勒是不可想象的，独裁不能传子韩国、智利、印尼的”傀儡“强人们只能眼巴巴看着金老二了。（关于毛，我觉得他也是想传子的，可以另文讨论）<br />
 <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;希特勒和斯大林建立的“极权社会“是20世纪自由世界最大的威胁，我不是想为美国辩护，我也觉得白宫里混蛋也比明白人多，但我更认为，从策略的角度考虑，美国培养”代理人“（”独裁傀儡“），是在冷战这个――这和罗马时代设立”独裁“的初衷是一样的。这些独裁者构筑一道抵抗共产极权社会的防波堤。被扶植的人肯定有一把问题，但其代价――至少在目前看是值得的，共产极权并没有扩散到这些”美帝扶植独裁傀儡”的国家，而且，在冷战的这个大背景结束之后，这些国家向自由民主国家的转变，也快了很多。退一万步讲，这些”傀儡国家&quot;里饿死的人，枪毙的人，也比极权国家少多了。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;回到文章中心，“美帝扶植独裁傀儡”这个词，用“独裁”来掩盖了“极权社会”。这本身就是一个词语对人的遮蔽的最好的例子。在独裁这个词下面，专制社会和极权社会的区别被取消了，一切变得“相对主义”了。这才是自称“左派”的那帮人最喜欢做的事情。<br />
&nbsp; &nbsp;&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp; &nbsp; （最后讲一个题外话，词语对人的遮蔽，是共产及其同情者最擅长的把戏，以独裁论，比如亲共的卓别林就把希特勒描述成为《大独裁者》，而根本不碰斯大林，再说斯大林，斯大林把自己的盟友希特勒说成“极右”，用词语来“切割”）</div>

							
							
							
							
