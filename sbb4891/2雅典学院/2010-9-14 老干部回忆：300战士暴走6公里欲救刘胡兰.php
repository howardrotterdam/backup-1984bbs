																											<div id="postmessage_829172" class="t_msgfont">老干部回忆：300战士暴走6公里欲救刘胡兰<br />
<br />
2010年09月14日05:28<br />
作者：胡利&nbsp; &nbsp;来源：大洋网-广州日报<br />
<br />
<img src="http://img.hexun.com/2010-09-14/124885955.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
李铭与老伴合影,展示自己过去获得的勋章。<br />
<br />
　　91岁老战士李铭揭开一段尘封历史　<br />
<br />
　　刘胡兰英勇就义63年后,一段尘封的历史被揭开。昨天,广东乐昌监狱离休老干部李铭说,刘胡兰被反动军队杀害前,他所在的部队曾派出300名战士进行营救。<br />
　　记者经狱警介绍认识南下老干部李铭。身板笔直、眼光有神的他,看起来也就七八十岁。但其家属表示老人家今年91岁,1944年参加八路军,曾在山西交城八分区独一师50团当兵。<br />
<br />
到达现场时刘胡兰已牺牲<br />
<br />
　　李老在几年前患过一次病,耳朵显得很“背”,但能比较流利地表述一些往事。当他看见写在纸上的提问,亦能有针对性地予以回答。李老说,他参加过游击队,攻打过太原。他参加八路军时是为了能吃饱饭,家里弟兄多,吃不饱,他才主动找八路军部队要求参军。征兵的人问他为什么要当兵时,不懂什么叫革命的他,也老实回答“为了有饭吃”。<br />
<br />
　　李老的三儿子李忠灵说,一生奔忙的老人本来有多枚军功章,因被缝进枕头保存,许多年前连同枕头被小偷偷走,而今只剩下六七枚纪念章。由于没有什么文化,离休后老人家没有执笔写过一篇回忆录,只是偶尔向家人讲起关于当兵的往事,包括他们营救刘胡兰的“急行军”。<br />
<br />
　　“跑啊跑,大家累死了,不过还得要跑。”老伴李运珍说,一心只顾工作的李铭从不跟人吹嘘自己做过什么,即使作为夫妻,李老也很少跟她提起自己辉煌的“革命史”。唯独提到1947年营救刘胡兰的事时,李老会说当时跑得很累,差点跑得趴倒在地。<br />
　　记者向李老询问营救刘胡兰经过的过程中,李老的记忆完全是片段式回放。李老说,记得部队决定要营救刘胡兰那天,他们出动了一个营共三个连,总人数为300人左右。因营长一声令下,他们开始急行军,从部队驻扎地一口气跑了12里（6公里）。可惜的是,他们到达现场时,敌人已逃之夭夭。<br />
<br />
　　“我看见她的头和身子各在一边,铡刀还摆放在那里。”李老说,面对刘胡兰牺牲的场景,包括他在内的不少新战士都不敢多看。见自己的部队来到,当地老百姓围了上来,问他们“下一步该怎么办”。考虑到敌人走远,追击没多大意义,部队开始往驻地回撤。新兵还被领导要求先行撤离。<br />
<br />
　　党史专家:刘胡兰牺牲后确有部队赶去<br />
<br />
　　记者昨天联系上文水县党史办原主任武秉礼。他说1947年1月12日刘胡兰牺牲之前,已有部队驻扎文水。1月11日,中共文水县委指示陈区长组织人员将刘胡兰转移至山区,但12日上午一大早,村子被敌人完全包围。刘胡兰牺牲不久,有一支叫“独二旅”的部队赶到了现场。<br />
<br />
　　武秉礼还说,当时文水县和交城县（两地相距约20公里） 都属于八分区,附近驻扎的部队很多。刘胡兰牺牲后,“独二旅”带走了一把带有烈士鲜血的泥土,以此激励攻打文水县城的全军将士。1947年2月2日,文水县城被“独二旅”等部队攻破。<br />
　　记者把以上情况转告李铭老人。而李老说,1947年他还属于部队的“新兵蛋子”,他只记得副连长和一个指导员的名字,也记得曾攻打过文水县城。希望更多知情人与本报联系,以便能还原“营救刘胡兰历史”的真相。<br />
<br />
　　史实链接:刘胡兰牺牲情况<br />
<br />
　　刘胡兰1932年10月生于文水县云周西村（现刘胡兰村）。她14岁任村妇救会秘书,并被吸收为中共候补党员。1947年1月12日,军阀阎锡山的部队突然包围云周西村,因叛徒告密,刘胡兰等人被捕。刘胡兰视死如归,以“怕死不当共产党”回答敌人的威逼利诱,终被敌人铡死。得知刘胡兰牺牲的消息后,毛主席为之提词:“生的伟大,死的光荣。”<br />
<br />
