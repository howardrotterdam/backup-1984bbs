给奶牛注射孕激素可以促进泌乳或者干脆让没怀孕的奶牛泌乳，但也可能抑制产奶。是抑制产奶还是促进产奶取决于浓度。一口咬定抑制产奶实在不像是一个生物化学博士该有的水平。</div>

							
							
							
							
