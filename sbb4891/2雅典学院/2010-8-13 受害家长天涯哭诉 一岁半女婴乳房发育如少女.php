一直有记者、读者问我对圣元奶粉事件的看法，我的回答是：一、如果某品牌或某批次的奶粉成分有问题，那么就会有众多婴儿受影响，而不会只有个别案例。二、性早熟有一定发病率，病因复杂，不能因为刚好吃了某种奶粉就认定那是病因。三、雌激素检测很容易，既然有怀疑，对该奶粉做个检测即可。<br />
