有人声称奶农给奶牛注射雌激素催奶（所以导致奶粉中雌激素含量高）。实际上，如果给奶牛注射雌激素，那将会抑制产奶。给奶牛催奶注射的是生长激素。生长激素是一种蛋白质，口服无法被人体吸收，而会被消化掉。即使有少量生长激素进入人体，也对健康无害。 ... </blockquote></div>方舟子这两段微博，前一段靠谱，后一段就有点白目了。<br />
