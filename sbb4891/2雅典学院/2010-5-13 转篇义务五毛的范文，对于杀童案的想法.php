<br />
 <br />
<br />
毫无疑问，当事人都是冲动型偏执心理疾病患者。他们日常生活很正常，恰恰掩盖了他们心理上的重大缺陷，从而在事后很多熟悉的人都根本不相信。该心理疾病有如下特征和原因：<br />
<br />
 <br />
<br />
1、伤他第一，自利第二。正常人行使打击伤害他人动机的时候，都自然会启动自利动机来进行利弊权衡。而当事人，都以伤害他人（或某群体）为唯一目的，而不是追求自己幸福为目的。诚然，我们可以泛泛抨击说上层社会剥夺了下层民众追求幸福的权利。但从这些个当事人来看，他们并不是走投无路的人。缺少对自己的认识和尊重，更缺少对他人的认识和尊重，是主要原因。源头，则可反思到我们传统文化对个人实现的漠视。传统文化里，幸好有着集体主义来抵消个人漠视的不良影响。而在当下时代，个人同样被漠视，并没有更高的价值观来补偿，从而造成精神上的迷失，不知道如何自我实现，如何追求自我幸福。<br />
<br />
 <br />
<br />
2、只求效果大小，不论手段恶毒。当事人追求的是最大的伤害效果，而没有任何对象和手段的原则。不迁怒无辜，盗亦有道，冤有头债有主，要有良心，等等，这些价值观类型的“限制”规则，都抵挡不了他们对效果的欲望。他们在心情好的时候，看到孩子们未尝不是笑眯眯的。是人就有这种天性。但他们缺的正常的“复仇”价值观。这个问题绝对是上层组织的责任。一来提供不了有效的法律途径实现复仇手段替代，二来长期谬传“不管黑猫白猫，抓住老鼠就是好猫”的世俗实用主义。其实大大小小的价值观，只要得到社会认可，都能约束那仇恨的怒火。如儒道，如基督，如穆斯林，如佛，如骑士，如武士道，如马克思列宁主义，如MZD思想，不管是温和传教方式还是粗暴洗脑。而我们现在是什么价值观呢？我们可以不要有宗教信仰，但要有价值原则。<br />
<br />
 <br />
<br />
3. 懦弱，无勇。复仇是人的天性，以弱小入手，说明了缺乏内心那种隐忍和无惧的勇气。其实只要有心，再有权势的人，也有漏洞。其实，这些当事人根本就不是复仇，而是泄愤而已。用什么来泄，就不重要了。这是我们久已丢失了的侠气精神。<br />
<br />
 <br />
<br />
说起来，我们群体，包括我自己，多多少少都患有此病。只是爆发点不同，运气不同罢了。从自我改善做起，同时影响周围人，或许会有些作用。虽然改善自我很痛苦，影响周围很麻烦。</div>

							
							
							
							
