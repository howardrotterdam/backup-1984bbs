为什么中国长期在封建君主专制的泥淖里挣扎？为什么中国人都是”愚民',&quot;贱民“，偶尔出一两个对现实不满的清醒人，还被扣上”刁民“的帽子大加鞭笞？我认为，正是由于儒家的所谓“文化”的“伦理”，也即儒家伦礼。给中国人的脖子上套上了条无形的狗链子，使中国人失去了对真理，是非，美丑的追求，使中国人永远都只能辨别、判断“亲疏”、“贵贱”、“尊卑”，像狗一般为尊者命是从，不问对错，只知维护”尊主“。并让他们之中的大多数全都只能听从“天命”的安排、听从“大人”的奴役、听从“圣人之言”的训导，从而安安心心地“修己安人”、安安心心地吃苦耐劳、安安心心地服从自己狗一般卑贱的命运、安安心心地当奴隶、作牛马、做一条忠实于主人的狗。而我们的历史，也早已告诉我们，在这样的文化下，中国人过的多么悲惨，多么愚昧。为什么中国人在共产党的暴政下依然安之若素不知反抗只知下跪，口口声声”自由民主“的杨恒式”民主儒徒“是思想上依然没有摘下愚昧的狗链而不知道反省过失，与邪恶儒教决裂，还是当狗当惯了，不愿意摘下脖子上的狗链子，追求真正的自由民主呢？<br />
