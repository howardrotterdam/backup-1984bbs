													<h2>中国领导人应该手按《论语》宣誓就职</h2>
						
						
																											<div id="postmessage_486696" class="t_msgfont">我把孔子比作中国的耶稣，因为孔子在中国的地位，如同耶稣在西方一样，也是在意识形态领域长期居于至圣先师的地位，对社会生活产生了重大的影响，他们的名字在一定程度上已经成了一种信仰价值系统的代名词，他们所代表的文化系统在历史上受到了方方面面的相类似的褒扬或者冲击。<br />
<br />
基督教的地位今天在西方的民主社会获得了极大的尊重，比如一个重要的象征就是美国总统就职的时候，是要手按《圣经》宣誓的，这样的传统已经保持了200多年。<br />
<br />
在历史上，西方关于基督教的争论也进行的异常激烈。在中世纪相当长的时间里面，基督教曾经对欧洲的社会生活与政局起到了极大的影响和决定作用。从文艺复兴以来，一边伴随着民主自由理念在全世界范围内的广泛传播与实践，西方最重要的一批思想家也首先对基督教进行了最深刻的反思与批判。但是在西方的普罗民众中，基督教徒的数量一直高达70-80%以上，他们的观念对社会生活起到了极大的影响作用。<br />
<br />
民主社会最大的好处就是允许自由的思想与言论，在西方社会里面，最重要的一批思想家对基督教进行了反思与批判的声音，与70-80%以上基督教徒的信仰声音达成了一种良性的平衡，而并非著名家们就可以代表一切。广大的基督徒一方面可以接受容忍新思想、新思路的进步，同时又在基于自身现有基础的前提下稳步前进，达成一种新的和谐。<br />
<br />
民主自由的社会里面，在给予人自由个性的发挥空间的同时，也对个人提出了巨大的挑战，这种人性的解放与自由，是文明的解放与自由，而不是兽性欲望的简单释放。一个完全没有信仰的社会是非常可怕的，在文明稀缺的地方，制度建设往往也成为了空谈。<br />
<br />
我们不能否认，在历史上，基督教与中国的儒家学说一样，曾经与专制政权相结合，成为奴役压制人民的工具。但是，从另外一方面来说，我们也不能否认的是她们也同样起到了缓冲强权的作用，并且通过长期的教化，为传播文明起到了巨大的推动作用。在这一点上她们两者有一个惊人的相似之处，就是早期的教育工作基本上是由教会和儒家来完成的。<br />
<br />
在从专制社会迈向民主自由社会的巨大社会变迁中，如何对待传统文化和文明，在这方面曾经有过成功的例子，也有过失败的教训。<br />
<br />
比如成功者中最典型的就是英美社会，英美两国的民主制度可以说从根本上来讲，都是新教徒奠定的。在这里有一个对传统文化的重新厘清和发展的工作需要进行，正如同欧洲之新教的发展，是在与天主教的对抗中完成进行的，是这个工作的具体表现，因为这里面正包含着对过去中世纪的基督教文化中专制糟粕因素的剔除，发展基督教中与新时代文化精神相匹配的部分，其中最典型的就是韦伯在其名著《新教与资本主义精神》中所指出的现代资本主义精神，就是基督教文化中的核心精神与时代特色相结合的伟大产物。<br />
<br />
但是在时代变迁的时候，还有一种态度，就是认为以基督教、儒家学说为代表的传统文化，在历史上曾经为专制统治者服务，在其中难免有麻痹压抑人民的成分存在，所以要彻底的否定它。这样的例子，前有法国大革命，人民为了走向民主自由，把宗教也彻底否定了。另有中国从五四到文革的惨痛教训。要知道基督教和儒家学说一样，可以说是几千年传统文明的集大成者，是人类文明的既有丰硕成果。伟大的物理学家牛顿在谈到自己的成就时曾经说道：我只是站在巨人的肩膀上而已。这是一句非常智慧的话，对文明也一样，只有站在旧有文明的肩膀上去发展她，抬升我们的视野，而不是拦腰斩断她，让人类跌进丛林法则的原始人性中去挣扎嚎叫。法国大革命、文化大革命中的共同特点就是否定基督教、儒家学说，这等于否定了一切传统文明，也就等于否定了文明。人没了文明，就如同回到丛林与原始状态，走向兽性、疯狂就是一种必然，想从这样的原始状态跳跃发展到高级文明状态的民主社会，根本是一种天方夜谭式的梦呓。<br />
<br />
所以英美社会发展出成功的民主模式，都是在坚持基督教传统的基础上，其中最典型的表现就是美国总统手按《圣经》宣誓就职，这代表对既有文明的一种尊重，正如《论语》里面说“温故而知新”，这本是社会发展最基本的逻辑，如同水有源流，树有根本，人有传承，这种发展有其连续性，彻底地打断存在着巨大的风险。<br />
<br />
儒家与基督教有相类相通的内核，就是仁爱精神、文明追求，这是一切成熟社会的基本素材，是解决人之为人的底线问题的。这两个文化系统中汇聚了人类几千年文明的结晶，无论我们怎样判断过往的历史，这些都是我们人类发展所不可轻忽的宝贵经验，放弃这些文明的价值的人是最大的傻子和疯狂者，因为他们目空一切到了以为一个时代的人的智慧可以胜过一切过往智慧的程度。其实一切时代的智慧归根结底中只有人的智慧才有真正的意义，而人的智慧只有建立在生活的基础上、体悟的基础上，所以大量真实的生活体验才是最有价值的，作为其代表的历史和传统文明才是最值得保存和珍惜的。<br />
<br />
当然，在现实中我们不能只停留在既有的文明中，而要积极学习先进的、发展更高程度的文明。但是，这只是为了发展出我们这一代的文明，而不是让我们有资格去轻忽任何一代的文明，我们越是生活，就越会发现任何一种生活的经历都是有价值、有经验意义的，更何况是许多时代的叠加。在这些时代面前，我们只能肃然起敬、铭记在心。当我们把手放到古代的典籍上宣誓的时候，我们更多地表达的是对过往生命的尊重，生命在本质上是相同的，以前的数量更加众多。如果有一天中国领导人手按《论语》宣誓就职，我想很多事情就自然和谐了。</div>

							
							
							
							
