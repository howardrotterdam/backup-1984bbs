													<h2>在孔府买了本有意思的书</h2>
						
						
																											<div id="postmessage_510086" class="t_msgfont">日期：2007-09-27 作者：伊人 来源：文学报<br />
<br />
初夏，上海作协散文组到泰安开笔会，除了登泰山之外，曲阜当然是必去的。我是第一回赴孔子故里，很惭愧，竟没有一点“朝圣”的激动心情。跟着导游进孔府，观孔庙，访孔林，一个直接而肤浅的感觉是:真大。用文诌诌的话来说是:三孔气象大矣哉！大无疑是必须的――历朝历代，时有皇帝驾临，卿相拜谒，祭祀盛典更有皇皇八佾之舞，不大如何显尊贵、壮观瞻？而况如鲁迅所说:“在那地方，圣裔们繁殖得非常多，成着使释迦牟尼和苏格拉底都自愧弗如的特权阶级。”弗大更何以沫圣荫、享尊荣？古书上说:“君子之泽，五世而斩。”夫子之泽竟能绵延两千多年，承露传衍至七十七世(孔德成)，这堪称是绝无仅有的世界奇迹了。<br />
<br />
&nbsp; &nbsp; 这回游三孔，要说个人感受中最不堪忍受而又不得不忍受的，就是“这是乾隆的……”、“那是乾隆的……”了。这个大搞“文字狱”、虐杀文人庶民令秦始皇都望尘莫及的爱新觉罗弘历，曾经多次“驾幸”曲阜，在“圣人之乡”作诗啦，题词啦，大过其“风雅”之瘾，几乎到处可见其“御制”遗迹。此外还留下一些肉麻当有趣的所谓轶闻，比如有棵歪树，说是因为乾隆倚靠了一下，那树皮便化作了龙鳞――“你们看，像不像呵？”导游用小旗指着那歪树问。我想说声“无聊”，可还是忍了。芳龄二十的导游小姐说这个，不过是给游客添趣助兴而已，这是她的活儿，也不容易。<br />
<br />
&nbsp; &nbsp; 要说此番游历的收获，也有，就是买了一本有意思的书。这本书名为《孔府档案选》，是在孔府里买到的。<br />
<br />
&nbsp; &nbsp; 进入孔府，见两旁设有几个书摊，卖的大多是关于孔子和曲阜的读物，于丹女士的那本自然是摊摊皆有，且放在醒目的位置。我却一眼盯住了封面灰暗的《孔府档案选》，拿起翻了翻，里面选录的是孔府原始档案，都是真材实料，不是随心所欲的什么“心得”。我喜欢真实的东西，就未加犹豫买下了。<br />
<br />
&nbsp; &nbsp; 这真是一本很有意思的书。而在孔府里面买到它，就更有意思了。<br />
<br />
&nbsp; &nbsp; 《孔府档案选》由中国文史出版社出版(2002年)，主编骆承烈是曲阜师范大学孔子文化学院的教授，潜心研究孔子及儒学凡五十年。这本书的前身是他参与主编的《曲阜孔府档案史料选编》(23册)，曾获国家级图书奖。据他说，上世纪五六十年代，最早参与整理孔府档案的颜世镛先生，是孔子爱徒颜回的七十七代嫡孙。这本《孔府档案选》的封面书名，则是由颜回的七十八代孙颜廷淦题写的。<br />
<br />
&nbsp; &nbsp; 而《孔府档案选》向世人展示的，却不是“圣人家”的光耀，倒是揭露出不少的阴暗和耻辱。但它绝不是什么“谤书”，因为书中所陈示的大都是孔府自家笔墨所写的真实记录。<br />
<br />
&nbsp; &nbsp; 翻阅着这本书，让我说什么好呢？我只能一个“唉”接着一个“唉”。<br />
<br />
&nbsp; &nbsp; 我实在不能不“唉”。且看――<br />
<br />
　　1644年清军入关，几乎兵不血刃地入主北京。九月初一，孔府衍圣公就奉上了《初进表文》，谀颂满清君主“承天御极，以德绥民”，什么“六宇共戴神君”啦，“八荒咸歌圣帝”啦，“山河与日月交辉”啦；还诚惶诚恐地自称“臣等阙里竖儒”，“今庆新朝盛治”云云。如此急切地向“新朝”献忠输诚，不能不令人吃惊。<br />
<br />
&nbsp; &nbsp; 想想大明朝老朱家待他们“圣裔”，实在是不薄呵！当年朱元璋称帝伊始，就赐给孔府祭田两千大顷(合六十万亩)，并配拨耕种祭田的大量佃户。朱元璋还特赐龙头藤杖给孔氏族长，令其“主理家政”。……可以说，明王朝对孔府“圣裔”真正是优礼有加，皇恩浩荡。我们且不说什么“民族气节”，不要求“圣裔”们效法顾炎武、史可法、刘宗周、张煌言等抗清志士；单以“知恩图报”的旧道德而言，“圣裔”们对刚刚覆亡的旧朝也理应深怀哀念，至少不必那么急不可待，抢在“第一时间”向“新朝”进表献忠吧。<br />
<br />
&nbsp; &nbsp; 投桃报李。孔府的献忠“进表”极快得到了回报――九月初四日，清廷就下旨:“先圣为万世道统之宗，礼当崇祀，昭朝廷尊师重道之意。”恩准孔府圣裔仍将享有既有的一切优渥特权。“新朝”顺治帝福临当时只有六岁，因此，这个优礼孔府的圣旨，其实是摄政王多尔衮和降清的范文程等大臣深思熟虑之后作出的。<br />
<br />
&nbsp; &nbsp; 第二年(1645年)闰六月，孔府圣裔又以实际行动，向“新朝”表示彻底归顺的忠心。这本档案选中有一篇《上剃头奏稿》，该奏稿称:臣(衍圣公)齐集上下众人，于闰六月二十六日，“恭设香案，宣读圣谕。……俱各剃头讫。……臣不胜惶悚战之至，为此具本奏闻”云云。<br />
<br />
&nbsp; &nbsp; 而恰恰就在这个时候，江南的嘉定、松江、昆山、江阴等地，千百万士人和民众正在为抗拒野蛮的“剃发令”而殊死斗争；从这年的闰六月初一至八月二十三日，江阴民众浴血抵抗了整整八十三天！当时有个在华的洋教士就曾为“中国人为保护他们的头发和服装所表现出的悲愤和战斗勇气”而感佩不已。当然，在“身体发肤，受之父母，不敢毁伤”的先圣祖训，和面对“留头不留发，留发不留头”的严酷现实之间，孔府圣裔可以做出他们的选择；他们也不一定要像那些江南人为了头发而不惜丢掉脑袋。时势所迫，性命攸关，剃发就剃发吧，但有必要“不胜惶悚战之至”地上奏朝廷吗？用“奴相”一词来形容，有人或许觉得刻薄，但我不知道还有什么词比“奴相”更为适合。<br />
<br />
&nbsp; &nbsp; 二百七十年后，已是民国时代，在两次不大不小的闹剧中，孔府圣裔又有不甘寂寞的表演。1915年12月袁世凯宣布恢复帝制，次年即为“洪宪”元年。《孔府档案选》里载有衍圣公孔令贻贺袁世凯称帝电文:<br />
<br />
&nbsp; &nbsp; “皇帝陛下圣鉴:窃维俯顺舆情，允国民之拥戴；仰承天命，建皇权之谟猷。……令贻等守先圣尊王之意，输瞻云就日之诚，不胜租砉奈柢跋闱熳Ｖ至。”<br />
<br />
&nbsp; &nbsp; 欢欣之色溢于言表。短短十多天里，袁世凯就四次召见孔令贻，并封其为“郡王”。档案选中还有一封族兄给孔令贻的信，族兄喜形于色地写道:“新帝待遇优隆，迭膺荣典，超越前代。……我邑自我弟晋封王爵，族中亲友，人人欢欣鼓舞，屡向兄讨要喜酒吃。”<br />
<br />
&nbsp; &nbsp; 可惜，袁的称帝闹剧仅八十多天就黯然收场，衍圣公的“郡王”梦当然也随之破灭。<br />
<br />
&nbsp; &nbsp; 一年多后，“辫帅”张勋帅兵入京，解散国会，拥戴溥仪复辟。孔府圣裔又急忙给张勋致电敬贺，称“日月重光，毅力殊猷，普天同庆”云云。这场闹剧更短命，只十来天就玩完了。圣裔们又空欢喜一场。<br />
<br />
&nbsp; &nbsp; 在我们看来那都是闹剧，而孔府圣裔当然不这么认为，在其心目中，那可是“守先圣尊王之意”的头等大事。孔令贻在给袁世凯的劝进电中明言“共和国体，既不适用于中华”；唯有“建皇权之谟猷”。游览孔府，至今可见到的对联中，有一句透出了“精义”所在:“与国咸休，安富尊荣公府第”。此所谓“国”就是皇权帝制之 “国”，孔府圣裔赖此而安富尊荣。至于那“国”的皇帝，姓赵，姓孛儿只斤，姓朱，姓爱新觉罗，姓袁……都可以，他们很懂得与“氏”(不同姓氏的皇帝)俱进。<br />
<br />
&nbsp; &nbsp; 一本《孔府档案选》，让我见识不少。能在孔府里买到它，真的很有意思。<br />
