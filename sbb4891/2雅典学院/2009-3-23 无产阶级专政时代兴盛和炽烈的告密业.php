													<h2>无产阶级专政时代兴盛和炽烈的告密业</h2>
						
						
																											<div id="postmessage_59574" class="t_msgfont">谁把聂绀弩送进监狱？<br />
<br />
作者：章诒和<br />
<br />
<img src="http://ww225w.chinaelections.org/UploadImg/2009_3/mc_191371765116.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
聂绀弩（右一）陪李健生（右三,章诒和之母）到萧军家医骨伤，在萧宅前留影，中为萧军夫人，拍摄者为萧军<br />
<br />
<br />
2008年春夏之交，谢泳从厦门出差到北京，我约了几个朋友一起吃早茶。边吃边聊，你一言我一语，无主题地东拉西扯。坐在身边的谢泳低声对我说：“最近，我看到一份关于聂绀弩的档案材料，很吃惊。”<br />
<br />
　　我问：“吃惊什么？”<br />
<br />
　　他说：“聂绀弩的告密者，主要是像黄苗子这样的一些朋友。”<br />
<br />
　　我瞠目结舌，半天回不过神来。事情太突然，太意外，太恐怖！<br />
<br />
　　谢泳说：“告密材料一直汇报上去，罗瑞卿批示：‘这个姓聂的王八蛋！在适当时候给他一点厉害尝尝。’”<br />
<br />
难以置信！我的脑子全乱了。<br />
<br />
　　一年后，我在2009年2月刊纪实版《中国作家》杂志上，看到了谢泳所说的《聂绀弩刑事档案》(简称“聂档”)，全文十余万字。作者寓真，系山西省资深政法工作者。他用事实说话，以解密了的档案材料为凭，系统又完整地揭示出聂绀弩冤案的真相。“去马来船相上下，长波大浪与纵横”（聂诗），我一口气读完，大恸，大悲。泪如大河，决堤而下。文中之人，我大多认识，甚至很熟悉。但一部“聂档”使他们的面孔变得模糊不清，甚至陌生起来。事实就摆在那里，一切都是无法回避，也无可辩驳：长期监视、告发聂绀弩的不是外人，而是他的好友至交。我必须认同作者的结论――聂绀弩入狱不是红卫兵扭送的，也非机关造反派捣鬼，而是他的一些朋友一笔一划把他“写”进去的。<br />
<br />
　　诗人邵燕祥看了“聂档”，内心非常沉重。他在最近发表的一篇文章里说：“今天的年轻人，看国外警匪片、国内电视剧，处处有线人、卧底、‘无间道’，谍影重重，英雄孤胆，看得紧张过瘾，甚至心向往之。他们想必是想象自己处于‘正方’，才能这般心安理得。他们不知道，他们的父兄一不是杀人放火的黑道，二不是走私贩毒的帮伙，却在很长时段里，曾经生活在被监控、被告密的恐惧之中……”（《牢头狱霸的前世今生》，载《南方都市报》2009.3.5）<br />
<br />
<img src="http://ww225w.chinaelections.org/UploadImg/2009_3/mc_191383080365.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
【图片：聂绀弩给黄苗子的诗】<br />
<br />
　　聂绀弩戴上右派帽子以后，发配到北大荒劳动改造，于1960年冬季返回北京。告密行为是从1962年开始的。也就是说，聂绀弩一言一行，一举一动，都是通过身边的人及时汇报上去，并进入专政机关的档案的。长年累月的告发检举，聂的问题性质日趋严重。依据事实，寓真把检举人分为两类。一类是戴浩（湖北人，电影家）、向思赓（湖北人，曾参加左联，1949年后为中学教师）、吴祖光（戏剧家）、陈迩冬（作家、时任人民文学出版社编辑），钟敬文（教授，民俗学家），他们与聂绀弩有着密切往来，到了“文革”时期，在人身自由被限制的情况下，被迫写有交代检举材料。另一类是几年来（1962―1967）一直“积极配合公安机关”的,包括王次青（先后在出版总署和版本图书馆工作）、黄苗子等。<br />
<br />
　　1962年9月12日递交的第一份密告材料开头是这样的：“我昨天去找了聂，与他‘畅谈’了一阵……一个晚上我得到了一点东西，破去不少钞，总算起来在20元以上了。兹将他的谈话，尽最大真实地记录下来。”这第一段话里，单是“畅谈”、“破钞”以及“尽最大真实地记录”几个词组，其主动性就不言而喻了。一共写了10页。这里截取聂绀弩谈论反右的片段：“你要杀人，你就杀吧，但是杀了以后怎么办？章伯钧一开始的时候就说：‘只要对国家、对大局有好处，你们要借我的头，我也很愿意。’要借我（指聂）的头，我也愿意，可是我话还是要说的。（着重，声激愤）现在搞成什么样子，他们要负责，全国都要负责，只有我们不负责，只有我们（手指连敲桌子）！”不得不佩服人家的记性和手笔，写得形神兼备。<br />
<br />
　　由于坐探当得出色，到了1964年，聂绀弩的反动言行和写作，就被频频搜集起来，摘编成专政机关的简报送到了高层。告密者行文如操刀，字字见血，刀刀入肉。于是，就有了那个“王八蛋”的批示。罗瑞卿还批示道：“聂对我党的诬蔑攻击，请就现有的材料整理一份系统的东西研究一次，如够整他的条件……设法整他一下。”<br />
<br />
　　到了1966年春的“文革”前夕，聂绀弩的“反动”言论已有上百页之多。内容有关于写作的，有关于文化的，更多的是对时局的议论。2月18日的材料汇报聂的言论如下：“现在农夫也不好当。从前的农夫向地主纳了地租之外，那块地怎么种，他有完全的权利。现在的农夫一点权利都没有……这样的制度是无法搞生产的。”“现在主要问题是人的权利问题，自由问题……”像聂绀弩这样的在野文人、失意墨客、当代清流，即使发配北大荒，也不可能“出世”。他们打探的是朝廷，挂念的是天下，感兴趣的是政事。聂绀弩只要与同类聚会，三杯酒下肚，那议论与牢骚就一起冒出来了。他思想敏感，独具慧眼，在惊人之语中，有深刻，有调侃，也有偏颇。这是中国文人需要的心理安慰，也是十分渴望的精神释放。<br />
<br />
　　都是几十年的朋友，都是头戴右派帽子，都是有才气的文化人，谁防备谁？时局尽管紧张，无奈聂绀弩是“潭深千尺歌尤好，酒满三巡肉更香”（聂诗）。好友加好酒，他说话就越来劲，话的分量也就越重。1965年8月4日，几个人在聂家一起吃晚饭。饭后，聂绀弩谈兴来了，大放“厥词”。他说：“有许多事情，我们会觉得奇怪，你想：一个普通人，总不能不看报纸吧，天天看报纸都看到自己怎样伟大，怎样英明，你受得了受不了？从个人来讲，不管怎么伟大英明，也总有不伟大不英明之处。从党和组织来说，不管怎样正确也总有不正确之处。都好了，都对了，都正确了，那就是什么呢？那就是完了。这是不可能的，是不辩证的。”我看得出来，寓真公布的档案材料是经过严格挑选、细心铺排的。由于众所周知的原因，那些异常激烈的言论，其实并未刊出。聂绀弩和我父亲（编者注：章伯钧）一样，在私人聚会的场合，会直呼其名，会拍桌子瞪眼睛地大骂，还会讲脏话。出语刻毒和文风犀利是等量的，都是思想光芒的投射！这才是聂绀弩。<br />
<br />
　　聂绀弩怎么会和这样一些人往来？理由太简单了：因为他只能和这样一些人往来，就像反右之后我的父母只能和罗隆基等人往来一样。1961年，聂绀弩刚从北大荒回京。为自己的工作安排，特意拜访老朋友、中国作家协会副主席邵荃麟。邵接待了他：斟了一杯酒，送了两包烟。随后说：“老聂，你不要再找我了，你的事我做不了主啊。”后来，聂绀弩写下这样的诗句：<br />
<br />
　　空屋置我一杯酒，也无肴核也无糖。<br />
<br />
　　其时三年大灾害，谁家有酒备客尝。<br />
<br />
　　举杯一饮无余沥，泪落杯中泪也香。<br />
<br />
　　临行两包中华牌：<br />
<br />
　　老聂老聂莫再来，我事非尽我安排。<br />
<br />
　　独携大赧出君门，知我何世我何人！<br />
<br />
　　知我何世我何人――读着这样沉痛的诗句，我能想象出聂绀弩的狼狈与赧然，能体味到他内心的屈辱和愤然。现实的处境及困顿，他只得与同类为伍了。<br />
<br />
　　因为都以现行反革命罪入狱判刑，我与聂绀弩是难友。1978年我出狱后，在聂家有一次痛饮和畅谈。我与他互相交换“案情”。<br />
<br />
　　他问：“小愚，你是因为什么进去的？”<br />
<br />
　　我说：“两条，一是反动言论，二是写反动日记。”<br />
<br />
　　聂大笑。说：“好哇，小愚和我犯一样的罪。我是说反动话，写反动诗词。”<br />
<br />
　　我说：“我的反动话，主要是攻击江青。”<br />
<br />
　　聂大悦。叫道：“李大姐（编者注：章诒和之母李健生），小愚和我恶毒攻击的是一个人！来，为了这个，我们要单独喝一杯。”<br />
<br />
　　我告诉聂绀弩：当时专政机关认为，章诒和光有别人检举的反动言论还不够，要把她钉死在罪行上，还必须有文字。于是，指使剧团造反派出面抄走了我的所有日记、札记、手稿，共17大本。他们终于找到所需的证据。白纸黑字，跑不掉了。聂绀弩也如此！“王次青写的检举材料，主要是关于聂的言论”，还需要白纸黑字的东西。这东西，就是诗了。诗是要人欣赏的，特别需要有鉴赏能力的人欣赏。所以，聂每有新诗，都要出示于人或寄赠好友。黄苗子既是识者，又是好友。“聂绀弩赠诗较多的是给黄苗子，但送给黄的诗篇，不知为何都进入了司法机关。”可惜，公安机关的人不懂诗，于是上面又指示：“这些诗要找一些有文学修养的人好好解释解释，弄明白真实的意思。若干典故也要查一查。”诗无达诂，古体诗含蓄、工整、优雅，内涵无穷的寓意。你可以从正面理解，他可以从反面来分析。大量的聂诗，找谁来破译？公安机关负责人还是聪明，说：叫诗的提供者来当诠释者。黄苗子也没有辜负他们，把每首诗里的“反意”都抠了出来。书中，寓真列出许多首诗。这里，仅举三例。<br />
<br />
　　冰道<br />
<br />
　　冰道银河是又非，魂存瀑死梦依稀。<br />
<br />
　　一痕界破千山雪，匹练能裁几件衣。<br />
<br />
　　屋建瓴高天并泻，橇因地险虎真飞。<br />
<br />
　　此间多少降龙木，月下奔腾何处归。<br />
<br />
　　这首诗作于北大荒。前面六句是描写利用冰道运送木材。问题是最后两句，大意是：当年为了保卫大宋江山，杨家将费了许多劲，去找降龙木，降龙木这种宝贝在北大荒这里却有的是。意指在那里劳动的“右派”都是天下奇才。但是，在这月色茫茫的夜里，一任它在冰道上滑走，它们将滑到哪里去呢？<br />
<br />
　　吊若海<br />
<br />
　　铁骨钢筋四十年，玉山惊倒响訇然。<br />
<br />
　　半生两袖多奇舞，一死双冠够本钱。<br />
<br />
　　不信肠癌能损尔，已无狱吏敢瞒天。<br />
<br />
　　只身携得双儿女，新妇飘零何处边？<br />
<br />
　　若海是指黄若海，青年艺术剧院的演员，1957年的“右派”兼反革命，在劳改中患肠癌，于1960年死去。诗意是：40年来你的身体像铁骨钢筋一样结实，可是忽然就死去了。你这半生是个演员，剧演得好（多奇舞），死的时候又戴着“右派”和“反革命”两顶帽子，真是够本钱了！我不相信单是肠癌就能要了你的命，是那些“狱吏”平日不早向上面报告，不替你医治，才使你丧了命！直到你死了，他们再不敢隐瞒上面了。可怜的是你那孤孤零零的妻子带着一双儿女，他们在这茫茫人海中飘零到哪里去呢？<br />
<br />
　　轱辘体之一<br />
<br />
　　紫伞红旗十万家，香山山势自欹斜。<br />
<br />
　　酒人未至秋先醉，山雨欲来风四哗。<br />
<br />
　　岂有新诗悲落木，怕揩老泪辨非花。<br />
<br />
　　何因定要良辰美，苦把霜林冻作霞。<br />
<br />
　　1962年秋，聂绀弩与麦朝枢（“民革”成员，戴过“右派”帽子，任人民文学出版社编辑）等游香山，麦以诗寄聂，中有“紫伞红旗十万家”之句，聂取之作轱辘体五首，这是其中一首。这首诗似有所指，有可能是影射国际或国内形势，主要意思包含在后面六句。大意是：在这深秋的时刻，秋风飒飒，山雨欲来的前夕，面对这落叶萧瑟的景色，伤感得写不出诗来，也怕拭清我这昏花老眼去辨认那些是非。秋天就是萧瑟的秋天，可是有些人偏要把它说成是美丽的，矫揉造作地把木叶冻作彩霞来装点这萧条世界。<br />
<br />
　　有了言论，有了文字，罪证齐备，抓捕聂绀弩的日子到了。山雨欲来风满楼，他是有预感的，钟敬文也劝他焚诗，聂绀弩有些慌张，开始烧诗，还跟别人（如黄永玉）打招呼：“你就骂我好了。骂我什么也没关系……说顶讨厌聂某人也可以，但你不必提到我做诗呀！”然而，一切都晚了。“四顾茫茫余一我，不知南北与西东”（聂诗），处于绝境的诗人，感到深深的孤独。<br />
<br />
　　用文化人监视、告发文化人，决不是我们这里才有的，也非今天才有。俄国沙皇尼古拉一世统治时期，不少审查官就是19世纪俄国作家。在德国，著名的海德格尔就对老师胡塞尔实施“无形”迫害。我们国家自先秦以来就有了告密制度，最有名的则是朱元璋的锦衣卫。极权制度是制造告密者的根源，统治者希望每一个人都是告密者，而每一个人又都可能被告发。这样，朝廷才便于监视和控制，政权才能有效打击异端，及时翦除异己，以巩固统治。“文革”期间的告密行为是在“革命”“正义”的旗帜下进行的，只要能够保卫红色江山，无论怎样告密，采取何种方法，哪怕是告发父母，哪怕是暗中窃听，都是好样的，也都是“合法”的。所以，告密者毫无负罪感。有关部门所网罗的告密者，大多是有特长、有才气、有成就，也有些名气的人。因为只有他们，才有可能接触到政坛人物、思想精英和文化大家。一旦你被盯上了，那么政治厄运就悄然逼近，自己还浑然不知。<br />
<br />
　　这里，我还要说一句，黄苗子永远不知道，就在他监视密告聂绀弩的同时，也有一个文化人在监视密告他。<br />
<br />
　　的确，聂绀弩平反后，依旧和告密者往来、吃饭、聊天、唱和。难道他不知道是谁出卖了自己吗？不知道黄某人曾给自己注诗吗？我知道他知道，他完全知道。1982年10月25日聂在给朋友的一封信里，这样写道：“我实感作诗就是犯案，注诗就是破案或揭发什么的。”我是过来人，对此深有体会。比如预审员问：“你说过周恩来喜欢孙维世吗？”一听，立马知道这句话，我是在什么场合、什么时间讲的，又是谁检举的。聂绀弩当然清楚谁是告密者。那为什么他毫不“计较”呢？<br />
<br />
　　作者寓真有十分中肯的分析：一个原因是戴浩、向思赓、吴祖光、陈迩冬、钟敬文等人的检举是在“文革”中聂绀弩遭关押后，被迫写出的。另一方面是由于聂绀弩的超凡绝俗，大度豁达。但是，我认为他的淡然处之，是因其内心有着更深的痛与苦，不可对人言的痛与苦。事情不是那么简单。聂绀弩出狱后，常常突然不讲话，一连数日向壁而卧。有一次，聂的夫人周颖来找我的母亲，说：“你快去看看老聂吧，我实在拿他没有办法了。”<br />
<br />
　　母亲带着我去了。聂绀弩翻身起床，并打发周颖去买熟食。周离开房间，一直沉默的他劈脸问道：“海燕（聂之女）的自杀，你们为什么不告诉我真相？”<br />
<br />
　　母亲沉默。<br />
<br />
　　“你知道海燕的遗言吧？”聂绀弩问。<br />
<br />
　　“知道。”母亲答。<br />
<br />
　　“她在纸上写的那句话，我会琢磨一辈子，除非我咽气。”<br />
<br />
　　母亲劝道：“老聂，你不要这样，事情过去了。”<br />
<br />
　　“李大姐，你怎么也说这个话！事情能过去吗？”他用手不停地戳着心脏部位，自语：“永远过不去。永远过不去！”<br />
<br />
　　母亲不做声。<br />
<br />
　　“你不说，我来说！她的遗言就是她的死因，李大姐……你说海燕发现了什么……”母亲听不下去，伸出一只手掌，断喝道：“老聂，不要讲了，我不许你讲。”<br />
<br />
　　所有的人都哭了。有的事是一般人想象不到的惨苦，而聂绀弩每日每夜地面对这个惨苦。你说，他还有心思去“计较”别人吗？聂绀弩死前说的最后一句话，就是：我很苦。“圣朝愁者都为罪，天下罪人竟敢愁”（聂诗），他在世，坚不可摧，他死后，精魂不散。<br />
<br />
　　聂绀弩去世后，出卖他的人写怀念文章，那里面没有一点歉意。<br />
<br />
　　人在阴影中呆久了，便成了阴影的一部分。有些东西靠生命和时间，是无法带走和冲洗干净的。即使抹去了，想必会在未来的某个时刻，以另一种形式与我们不期而遇。<br />
<br />
　　2009年3月泪书于北京守愚斋&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; <br />
 <br />
来源：南方周末</div>

							
							
							
							
