													<h2>我为什么支持朝鲜半岛统一</h2>
						
						
																											<div id="postmessage_692407" class="t_msgfont">6月16日凌晨，朝鲜对巴西那场球赛，我是在单位加班时观看的。巧合的是，当期杂志封面讲的就是朝鲜边防军枪击中国人的故事，由我策划并编辑。<br />
<br />
看完那场球赛后，东方破晓，我拖着疲惫的身体回家，内心里却有一股复杂且强烈的情绪在翻腾，想写点观后感，再说说朝鲜。由于杂事耽搁，今晚方有空提笔。<br />
<br />
在那场比赛开始前，赛场奏响朝鲜国歌时，郑大世泪流满面的样子令人印象深刻。虽然很难准确说他因何流泪，但由于长期关注朝鲜，我还是相信自己能大概猜出他的心思。当祖国在国际上被严重打压，民众屈辱感往往也较强，就难免会有国民在国际场合表现出强烈的爱国心来。<br />
<br />
金氏王朝的独裁统治，在外人看来极不人道，但在其国内民众的心里恐怕会有复杂的感受。很多人被洗脑，对外部世界一无所知，无从比较体制的优劣，身处专制牢笼却以为是理所当然；有一些人可能会有不满，但金氏政权毕竟是联合国承认的，虽说国家和政权从理论上讲是两回事，但在实际中往往合二为一，难以完全区隔。<br />
<br />
不独朝鲜民众会遭遇此等情感上的困境，但凡专制国家，皆是如此。“爱国家不等于爱政权”说起来容易，实践起来却很困难。即便是独裁的政权，它也不可能完全只为己谋私，为了延续自己的统治也会考虑到国家的利益。例如朝鲜孜孜以求的“核武梦”，固然有助于巩固金氏王朝的统治，但又何尝没有捍卫朝鲜的国家利益？显然，处在中美俄等大国的夹缝中，小国如果掌握了核武，就有了讨价还价的相当本钱。<br />
<br />
此种心理不独金正日等统治者所有，朝鲜民众也不能例外。还不仅如此，据权威专家透露，韩国的军方人士私下里也大多数希望朝鲜核武梦成真，因为朝鲜半岛统一后，这个有核武的小国面对大国时，将拥有更多的底气。<br />
<br />
这也就是小兄弟不惜冒犯老大哥的主要原因。不听劝阻“悍然实施核试验”，致使中国外交形象工程――六方会谈“休克”。不过从反面想想，中美等大国不就是拿朝鲜和韩国作为棋子，在下“一盘很大的棋”么？作为一个主权国家而非附庸国，是有尊严的，朝鲜人（还有韩国人）凭什么要事事听命于人？<br />
<br />
说实话，不管朝鲜人和韩国人之间有着怎样的隔阂和误会，人家毕竟是一家人，当年被一些大国生生划分为两个国家，至今不能统一。要是我，肯定会恨死这些为了自己的利益而牺牲他人的“强盗”。<br />
<br />
我敢打保票，生活在朝鲜半岛上的人民，绝大多数对美国和中国不会有什么好感的。一旦朝鲜半岛统一后，任何国家都休想在上面驻军，甭管是以什么名义。<br />
<br />
我对朝鲜人民怀有深深的同情，尽管这种同情可能得不到被同情者的理解。因为地缘政治的缘故，因为国家统治者的缘故，他们作为一个整体是受到轻视的。如同当年因落后而挨打的的中国，其人民是被西方人轻视的。<br />
<br />
“东亚病夫”早没人叫唤了，但中国人即便是今日在世界上得到了充分的尊重，平等的对待吗？<br />
<br />
由己及人，更为朝鲜民众鸣不平。他们是人类中的一分子，本该也像其他国家中的人们一样，享有免于匮乏和恐惧的自由。但现实是，他们被体制所“囚禁”，他们很多人连饭都吃不饱，更别奢谈什么言论自由了。<br />
<br />
我曾求证一位在朝驻站几年的同行，朝鲜国内情况是否真像某些西方媒体所描述的那样不堪？他没做丝毫停顿地回答，“一点都不假，朝鲜人的生活状况可用一个词形容，水深火热。”<br />
<br />
正是从人道主义的立场，而非从地缘政治出发（当然也有考虑），我大力呼吁朝鲜半岛统一。也正因此，我希望金氏统治趁早结束，呼吁中国政府放弃对它的支持。为了所谓的大国利益，牺牲小国民众的幸福，是极不人道的，是该受到谴责的。这个谴责的对象也包括美国在内。<br />
<br />
对巴西的那场比赛中，朝鲜球员表现出来的友善，也打动了我。他们彬彬有礼，很少有严重的犯规动作，即便和对手有轻微的身体碰撞，也都是面带微笑。<br />
<br />
整场比赛，朝鲜球员坚毅的拼搏精神，对抗时的君子之风，让整个世界看到了一个不一样的朝鲜，这和那个“穷兵黩武”的“邪恶国家”的固有印象，有着本质的不同。我相信，很多西方人会因这场球赛，多多少少改变对朝鲜的看法。<br />
<br />
朝鲜问题当然是复杂的，也是短时间内难以解决的。但是我相信，不管是什么样的死结，最终都应该也必须被解开，因为，朝鲜人民的幸福是首位，其他皆应该让位于它。一切阻碍这个进程的“大国权谋”，都应该受到谴责！</div>

							
							
							
							
