													<h2>谢亚龙案大得让人吃惊</h2>
						
						
																											<div id="postmessage_828700" class="t_msgfont">2005年2月18日，谢亚龙驾着他那辆低调的蓝色POLO驶进了中国足协，也驶上了一条不归路。在外人看来，谢亚龙胆小、平庸，绝对不是那种大贪的形象。但从目前的种种证据来看，在柔弱的外表下，“龙王”却有着一颗勇敢的心，他的犯罪金额甚至远远超过了南勇等人。据知情人透露，谢亚龙的事儿很大，大得让办案人员都吃惊。在得知谢亚龙涉嫌受贿或操纵比赛后，足协官员表示其涉案金额超过200万，并且感叹他比南勇还要阴。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;(博讯 boxun.com)<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;敛财手法<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;1、指派官哨操纵比赛<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;谢亚龙被立案侦查的一大罪状就是操纵比赛和赌球，涉及职业联赛的夺冠、保级，以及全运会足球赛。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;在此前谢亚龙等人被警方带到沈阳协查后，圈内人士纷纷预测他的问题不是很大，最主要还是帮助专案组核查证实南勇、杨一民案件中的具体细节。然而从目前情况分析，足协很多工作人员都对谢亚龙看走了眼，一名官员就坦言龙王隐藏的太深。据悉谢亚龙和南勇等人一样涉嫌操纵比赛。此前媒体关于足协官员操纵比赛的手段报道比较多，比如通过安排“官哨”利用规则照顾行贿俱乐部，甚至包括操纵国家队比赛的结果来达到利用赌球赚钱的目的。具体实现操纵比赛的过程较为复杂，足协内部往往会有一个联盟来共同完成，每个人都有不同的任务，谢亚龙也属于其中的一个重要环节。据了解有些球队为了在争夺冠军过程中占据优势，往往通过向足协高层行贿来操纵几场比赛结果，得到自己想要的分数。足协高层在拿到钱后会示意主管裁判的负责人通过委派“官哨”达到目的，然后他们再共同分掉这笔赃款。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;曾经有多家俱乐部现任或者前任负责人接受过专案组调查问话，其中还包括3支曾经夺得过联赛冠军的球队，专案组主要是想调查清楚俱乐部和足协官员之间是否存在权钱交易关系。而且今年年初，总局反腐倡廉领导小组也曾经分别找过足协官员谈话，询问是否知道俱乐部之间的问题比赛。除了控制联赛，全运会也是足协领导人比较好的控制和敛财的赛事。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;2、操纵国字号球队比赛<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;有一点可以肯定，在中国队的国际比赛上，肯定存在着巨大的黑金交易。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;除了五朝元老，中国足协下属的福特宝公司总经理邵文忠被带往沈阳协查两周之外，各地承办过中国队比赛的地方协会的领导，也被协助调查，主要就是针对黑金交易。也就是各地为了申办中国国家队的商业比赛，向足协领导人送钱送礼，并将最终的利益进行分成。另外，“进去”的人交代，在某些中国队的比赛中，存在贿赂裁判等行为，目的即是为了操纵比赛，然后通过赌球赚钱。赌球赚钱，才是操纵国际比赛最终的目的。在警方掌握的南勇、谢亚龙等人操纵比赛的线索中，不仅有操纵国内比赛，也有操纵国际比赛。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;有一种说法，早在2008年6月份，杜伊率领的国家队征战世界杯预选赛，主场打卡塔尔和伊拉克的时候，南勇和谢亚龙就曾经派一名国内裁判去做裁判的工作。开始，对方坚决不见，见了之后义正词严拒绝这名裁判的“意思”。最后，中国队连输两场，丧失小组出线权。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;此外，在国字号队伍比赛地的选择，以及赞助商的最终确认上，谢亚龙就多次独断专行强行着地，其中必然包括一些黑金和利益交换问题，这也同样是警方立案侦查的主要内容。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;3、收受国字号球员贿赂<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;在已经“进去”的人之中，南勇、杨一民、谢亚龙、蔚少辉等人，都管理过国字号队伍。种种迹象表明，“腐败窝案”已经牵扯出国字号丑闻：相关官员收受国脚和俱乐部贿赂。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;在国字号的利益链条中，收受球员教练乃至俱乐部的贿赂，是当权者最为简单的圈钱方式。有消息称，谢亚龙当权时，贾秀全为了获得国青队主帅的位置以及进入2008国奥队教练组，向谢亚龙“进贡”。当然，是否属实还要等专案组的调查结果。腐败不可怕，可怕的是足协的“一把手”陷入这种连环套之中，在培育腐败温床的同时，充当了腐败的保护伞。南勇和谢亚龙就是最好的典型，他们两个人涉及的领域几乎完全相同。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;知情人士透露，到足协最晚、“上道”却很快的谢亚龙，其“出手”的范围居然不比南勇少。在谢亚龙任上，各级国字号队伍的教练组更迭很频繁，数十名教练进进出出，这方面的黑金可能达到数百万之巨。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;据悉，教练员进国字号，之所以敢下血本，原因很简单――他们又会从球员那里赚回来。此次前国足领队蔚少辉出事之后，几乎所有人都联想到蔚少辉很有可能是收受队员的贿赂，不过据一位知情人透露，有相当一批国脚是在送出了巨额的“进队费”之后才穿上了国脚的外衣。而这些巨额进队费显然不是进了蔚少辉一个人的腰包，因为最终可以拍板定夺国家队球员的正是当时掌控国字号的谢亚龙。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;据悉，国字号球员的进队费用大概在10万到30万之间，但正是这一个又一个的10万、20万或是30万，也让中国足球的代表，国字号球队越踢越差，丢尽了中国人的脸。可以说，谢亚龙等人为了一己之私，损害了国家的利益。<br />
