																											<div id="postmessage_160339" class="t_msgfont">既然大家基本上对TG行将垮台已经达成了共识，那么我们就可以讨论一下它垮台的可能方式了。<br />
第一种方式：暴力革命。这种方式基本没有什么可能性。因为现在TG控制着军队，军队的装备以及军人们有素的训练保证了没有一支力量可以起来反抗，更不要说推翻它。近年来的若干群体事件为这一推测提供了佐证。<br />
第二种方式：TG良心发现，自己下台，就像当年东欧诸国。这种方式的可能性也基本不存在了。因为中国的政局和当年的东欧不具备可比性。经过改革开放30多年来，TG利用它的逆淘汰体制，基本上已经把体制内的好人，体制内的反抗力量已经清除的差不多了。就像昆虫会因为农药而增加抗药性一样，苏东波的巨变也导致了TG统治方式的升级。中央政治局那九个老头子，就算有人有心给中国带来民主和自由，那他也会被另外几个老头子掐掉，他的声音都没有发出来的可能。现在TG的掌权者完全是一个利益集团，他们控制着国家的经济命脉，不停往自己的腰包里A钱。<br />
第三种方式：外敌入侵，伊拉克模式。这种模式实现的可能性也极小。因为中国和伊拉克不同，伊拉克和中国政府虽然都是倒行逆施，但是程度不同，并且中国力量强大，拥有核武器，没有外部力量可以进入。<br />
第四种方式：缓慢演变。这种演变的可能性是存在的，尽管TG的体制有“拒腐防变”的能力，但事实往往不以他们的意志为转移。随着人们公民意识的觉醒，缓慢演变的可能性并没有完全堵死。<br />
第五种方式：军事政变。窃以为这是最有可能的方式。因为前面的几种方式实现的可能性都极小，所以不能排除军队中有真正具有良心的军官存在，或者是具有一定公民意识的投机分子的存在。随着TG倒行逆施的程度的增加，军事政变的正当性越来越高。当这种正当性高到一定程度，发生就几乎是必然了。<br />
