成为“父母皆祸害”小组的一员后，方馨不再纠结于自己的“大逆不道”。和这个根植于文青大本营豆瓣网之内，拥有近7000名成员与一个惊悚名字的讨论小组相比，方馨与父母的“战争”就“交火”的观赏性而言，简直平庸得不值一提。<br />
小组连组规都充斥着旗帜鲜明的战斗性：我们不是不尽孝道，我们只想生活得更好。在孝敬的前提下，抵御腐朽、无知、无理取闹父母的束缚和戕害。那些击中两代人关系痛点的帖子，总会迅速引起强烈共鸣，及上百条跟帖。<br />
与方馨在现实生活中展开的“战斗”一样，这些虚拟的硝烟都是单方面的。据组内调查，该小组成员的普遍身世背景为：生于1984之后，倾向于相信世界是以自己为中心的；从小便开始接触网络，没有什么不可接受的观点，也没有什么不容置疑的权威。<br />
而他们的父母多为小学教师，最爱看CCTV，祸害，这个词对于他们而言，只用来形容过万恶的旧社会，美帝国主义，以及麻雀；极不擅长使用网络，几乎没有可能加入到这个小组，朝儿女们打一场自卫反击战。<br />
这些在上世纪八九十年代常被喻为“人类灵魂工程师”的父母，在他们子女眼中，却是一群“僵化的国家教育机器的最末端执行者”，他们“逃得掉沉闷无趣的小学，却永远也别想从父母那儿毕业”。<br />
