他们成了不以个人意志为转移、既亲近又最疏远的父与子、母与女。<br />
<br />
“顺我者昌逆我者呸”<br />
互联网新生代热衷投奔志同道合的组织，或者，干脆自己成立一个组织，崇尚腔调，语不惊人死不休。<br />
“父母皆祸害”小组只迎来过一名“敌军”，一位高一女生的母亲，很快被驱逐。<br />
<br />
“父母皆祸害”出自英国作家尼克・霍恩比的小说《自杀俱乐部》，少女杰丝在姐姐出走后，与陷入神经质的母亲及任教育部长的父亲关系愈发紧张，在小结自己失败的青春期时，杰丝如是说。2006年，张坤将这本小说翻译成中文，并推荐至豆瓣网。<br />
2008年1月，一名豆瓣网友邀她加入其新成立的小组，并担任小组管理员，组名就叫“父母皆祸害”。<br />
张坤接受了任命。作为资深豆友，在上海某英文报任文化记者的她并没觉得这个小组离经叛道，她同时还在混“皮肤过敏”小组，“自取其辱”小组，“我们都是拖延症”小组……<br />
这些正流行于各大社交网站的小组，被公认为是互联网新生代的人际网络与生活方式，他们热衷投奔志同道合的组织，或干脆自己成立一个组织，崇尚腔调，语不惊人死不休。<br />
张坤认为，这不过是年轻一代争取话语权的一种方式。她来自孔子的故乡山东，家乡街道的墙上至今还反复刷新着“二十四孝”的内容，那堵墙，据说曾祖父那代起就一直竖着。“父母皆祸害”小组成立两年后，组员发展至近7000人，规模在豆瓣属中等偏上。话题每天都在更新，主题旗帜鲜明地集中于一点：如何对付父母？首页上，几篇被视为经典且指导性强的帖子长期置顶――如何高效地anti-parents（反父母）？让我们不要重复他们的失败人生；你是否在重复父母的错误……<br />
父亲节前夕，另一个热衷玩投票的社交网站开心网做了一个关于两代人关系的调查，投票结果显示，50后一代父母最大的问题是：控制欲太强，不尊重孩子隐私。<br />
