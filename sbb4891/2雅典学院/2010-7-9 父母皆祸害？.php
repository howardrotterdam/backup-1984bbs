有目击者称“父母老拿班上年级里第一名跟他们比，我经常在老师办公室看到他们哭丧着脸的子女，而且完全不管周围有多少人。”<br />
方馨从反抗父母不让熬夜看书、不让涂指甲油等鸡零狗碎的“游击战”升级至一场有预谋、有计划的“持久战”，分水岭出现于小学高年级。<br />
在一节教授鲁迅作品的语文课上，方馨举手向任语文老师、班主任的母亲就一处表述提出质疑，“要么是鲁迅写错了，要么是国家教委编错了！”母亲当场发飙，“鲁迅怎么会错？！国家教委怎么会错？！”<br />
坚决不认错的方馨最终受到最高处罚――在全年级面前高声朗读检讨书，重复三遍，“是我错了！”<br />
尽管成绩从小到大一直保持优异，方馨的奇思怪想仍然让母亲时刻担心。<br />
方馨父亲是铁路火车司机，按规定的时间睡觉，规定的时间起床，规定的速度开车。父亲性格温和、隐忍，惟一不可忍受的是，既定的生活节奏和规矩被打破。比如，方馨看书看到兴头上，总不愿按时睡觉、吃饭。<br />
这是一对50后父母的典型面孔――<br />
自有记忆开始，他们便接连经历各种政治运动、天灾、人祸，国家政策与生存需要使他们各自拥有众多兄弟姊妹，只有互帮互助才能生存下去，这培养了他们对集体主义朴实而忠贞的信仰；<br />
他们一家生活在铁路家属区，那是一个自成系统的独立王国，从铁路运输法院到医院到小学、中学一应俱全，这导致他们天生对组织与单位持以高度的依赖与信任；<br />
他们喜欢用“不三不四”来责怪方馨的非主流想法，在他们的认知体系中，世界本来非黑即白，不姓“社”就姓“资”，他们从小就被告诫只能以辩证唯物主义武装脑袋，解释经济、政治、美学、新闻……<br />
他们年少时很快就被剥夺了受教育的权利，日后在80后子女面前，他们之间可以交流的话题，和路人甲乙在旅行途中相遇时的寒暄，没多大区别；<br />
最严重的问题在于，他们已习惯于生活、就业、房子、生育统统被国家有计划地安排好，他们也就习惯性地想为下一代安排好一切。<br />
周瑞，武汉某名校新闻系本科生，父母均是湖北省教育系统官员，利用手中人脉，为女儿铺好从幼儿园到念研究生，甚至就业的路。他们对女儿越来越减少回家的次数，尤其对其试图脱离安排感到失望和不解。<br />
在他们看来，对女儿的爱，就是为她的未来铺平道路；而在女儿周瑞眼中，爱即放手，因为“他们的时代已经过去了”。<br />
初中毕业后，方馨成功考入外地重点高中，如愿以偿开始独立生活。每周，方馨都会收到母亲寄来的信，那是一种标准小学生作文体的文风，比如，“香港准备回归祖国大陆了，我和你爸爸都为是一名中国人而感到自豪”；“你即将迎来高考，我们都为你的刻苦学习精神感到骄傲而又担心……爸妈相信你一定会长成国家的栋梁”……<br />
在“父母祸害”小组里，父母是小学老师的豆友回忆往事，怨气难平，“卷子还是热的，我妈就把我叫去大骂一顿”，也有目击者称：“老师的孩子特别倒霉，父母老拿班上年级里第一名跟他们比，我经常在老师办公室看到他们哭丧着脸的子女，而且完全不管周围有多少人。”<br />
在他们看来，小学老师，不管是被迫无奈或主动随着教育大机器把活泼聪明的孩子教“死”，他们最深重的罪孽在于“自己被洗脑就算了，还对小学毕业N年的儿女进行持久性的无孔不入的祸害”。<br />
<br />
这时代与那时代对峙<br />
每到新闻联播和春晚时间段，方馨都刻意坐在父母身边，尽可能自然地对父母进行“思想启蒙”。<br />
作为管理员，张坤的职责是删除带人身攻击的过激帖，调节组内气氛。不过经她手的删贴不多，在她看来，这个小组存在的最大意义就是发泄。“许多人的困境高度相似。我们两代人之间，不是杰丝与她当教育部长的爸爸之间的问题，也不是她出走的姐姐与母亲之间的纠葛，”她说，“而是一个时代与另一个时代的对峙。”<br />
父母有他们停步的原因。毕竟掌握这个时代话语权的，仍然是与他们同龄的佼佼者。方馨、周瑞父母至今看得最多的电视节目，仍是新闻联播、春晚、黄金时段的主流电视剧；读得最多的报纸，仍是当地党报、都市报。<br />
其所延用的腔调、风格与主流价值观，成为两个断裂时代之间最稳定、变化最小的领域。<br />
2000年，方馨考入北京某名校。两年后，父母到北京看她。方馨正想着该带他们去宋庄转转，还是到东直门附近的各剧院看场话剧，母亲却要求先去天安门和毛主席纪念堂。<br />
在纪念堂的长队前，方馨陪着父母夹在一长串四五十岁的叔叔阿姨中间，莫名感到一种不自在。终于站在毛主席的水晶棺前，方馨发现母亲哭了。“她说，当年我作为红卫兵串联到北京，还幸福地亲眼见到他在天安门上招手。”<br />
之后每年寒暑假回家，每到新闻联播和春晚时间段，方馨都刻意坐在父母身边，尽可能自然地对父母进行“思想启蒙”――<br />
“爸、妈，我从学校带回几张碟，意大利导演托纳多雷的作品，我们看电影，不看春晚了行么？”<br />
这一烙印还表现在――<br />
母亲像患有强迫症似的，每次非要将粘在电饭煲、碗沿，甚至掉在桌上的饭粒收集起来，吃掉，才算松口气；<br />
父亲像跟电有仇，会定时检查家里还开着哪些不必要的电器，每天晚上七点钟，父亲只允许客厅亮一盏台灯，在昏黄的光线里一动不动地收看新闻联播。<br />
在方馨的“持久战”渐渐消沉下去时，22岁的江烨刚展开与父母的“战争”。2006年，江烨保送浙江大学，在认识几位志同道合的校友之后，她开始海量看书、看电影、泡豆瓣。<br />
在豆瓣她的首页上，记录着数百本已读过的书，数百张已看过的碟，数百名豆友，近百个常去小组――当然包括“父母皆祸害”。志同道合，而非血缘关系，成为她最重要交际标准，她拥有好几名密友，彼此分享最私隐话题，即使是他们从没见过面。<br />
父母对她大学之后远近亲疏的变化大为光火，“在他们看来，没有比家庭成员更重要的人了，”江烨语气中颇带自豪，“而我却偏要在家庭聚会上早退去见网友，在他们看新闻联播、春晚的时候上网。”<br />
在就业选择上，两套逻辑有如鸡对鸭讲。最终的妥协方案是，江烨必须保证毕业后进入一个稳定的单位，最好是公务员。<br />
“虽然我没办法跟他们解释清楚，‘单位’这个词是在英文中找不到对应的词儿，即将从我们生活中消失的概念。”江烨说。<br />
<br />
儿女是他们惟一的信仰<br />
方馨认为，宗教能让她内心平静，从容面对生老病死。而在父母看来，“信上帝有什么用？”<br />
如今，“矛盾就这么轻易地被一团肉乎乎的婴儿解决了。”方馨笑说。<br />
2004年方馨大学毕业留京工作后，一度缓和的家庭关系又开始变得紧张。缘由是父母执意搬到北京同住，将家乡的房子卖掉，供女儿女婿提前还完房贷。“好几十万的高利贷啊，”父亲至今都不习惯讲商业贷款，“欠人家这么多钱，你们晚上睡得着吗？”<br />
对金融工具的抵触，是50后父母的生理性反应。除掌控着当下话语权、最优社会资源的同龄精英，绝大多数普通的50后父母至今还住在单位的福利房里，不习惯欠人钱，不用信用卡，不敢预支未来。<br />
眼下，他们却又纷纷承担起奋斗在一线城市，即将成家立业的子女们最头疼的立足成本――房子。开心网曾组织过一次在一线城市买房调查，80%的参与者承认，首付甚至全额支付房款，来自于父母。<br />
在大多数情况里，50后父母，而不是他们刚出道，微薄工资还不够填厕所地缝的80后子女，成了天价房时代的“房奴”。<br />
方馨与先生抗拒用父母的钱，对长期共同居住也感到纠结。来北京居住不久，母亲就开始催方馨生孩子，并在做完一切可以抢到做的家务之后，会拿出随身携带的一团毛线，开始为不知何年何月才会诞生的外孙织毛外套。“以前，集体主义是他们的信仰，现在，儿女成了他们惟一的信仰，”方馨说，“母亲总说，父母活着的意义，就是为子女服务。”<br />
有更极端的“为子女服务”的故事，梁小利，生于1984，家乡偏居广东农村。大学毕业在佛山谋到一份教师工作后，在家务农的父亲却因为了给在大城市上学的女儿攒学费，长期在烈日暴晒下干体力活，被查出患上黑色素瘤，一种皮肤癌症，晚期。<br />
方馨最终决定要孩子，缘于计划外的怀孕，方馨父母也意外体会到女儿没入党的“好处”。大学期间皈依天主教的方馨无法接受堕胎。<br />
这点一度成为方馨与父母之间的严重分歧。方馨认为，宗教能让她内心平静，从容面对生老病死。而在父母看来，“信上帝有什么用？”而且，早年的教育让他们坚信，一切宗教都是唯心主义。<br />
如今，“矛盾就这么轻易地被一团肉乎乎的婴儿解决了。”方馨笑说。<br />
生产后，方馨将大部分精力投注到了女儿身上――托同学从香港快递奶粉，甄选婴儿早教机构，极力游说父母，还是别一次性还清房贷，将手头余钱买套学区房，尽力让女儿从小学到大学，都能上北京最好的学校。“母亲那时就笑了，”方馨说，“说我还不是和他们一样，将自己期待的模样，早早就强行施加到了她可怜的外孙女头上。”<br />
当妈妈的方馨每天上网的时间大大减少，她也很少再去逛“父母皆祸害”小组了。“父母皆祸害”管理员张坤注意到，两年里，小组成员的流动性很大，活跃的组员总是一拨一拨的，每天都有新人加入，每天也都有人悄悄离去。<br />
江烨还在里面潜水，绝大多数情况下，她就看着，不说话。在现实生活中，她一如既往地是父母的骄傲与精神支柱。和几乎所有组员想的一样，她这辈子都不会告诉父母世界上存在过这样一个小组，而且，她还是其中的一员。<br />
<br />
(应采访者要求，方馨、江烨为化名)<br />
