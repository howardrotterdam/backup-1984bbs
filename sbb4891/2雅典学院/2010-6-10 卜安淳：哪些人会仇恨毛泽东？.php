																											<div id="postmessage_673226" class="t_msgfont">卜安淳：哪些人会仇恨毛泽东？ <br />
（新人请注意发帖人和作者是两个不同的人，如果卜安淳也叫NERV那我认栽）<br />
<br />
标签： 毛泽东 <br />
● 卜安淳&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp; 电视连续剧《西游记》已有新版。张纪中版的新《西游记》尚未见到，浙江版的新《西游记》与央视版的老《西游记》则有许多的不同。浙江版《西游记》加进了更多的打斗，也加进了许多新的台词。打斗多些则热闹，台词讲究则有趣。我一向觉得，吴承恩笔下的许多段落是可以当作相声段子欣赏的。尤其是悟空、八戒之间的趣话，特别具有相声趣味。浙江版《西游记》重视了这种趣话，且有所发挥，其艺术效果是应当肯定的。 ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> ) <br />
&nbsp; &nbsp; 《西游记》的新老电视剧有个共同特点：删去了吴承恩笔下“唐皇游地狱”的内容。在吴承恩的《西游记》中，“唐皇游地狱”是特别重要的章节。唐皇李世民之所以出宫女、纵囚犯，就是因为他曾游地狱；之所以差遣唐三藏前往西天求取大乘真经，更是因为他曾游地狱。更重要的是，吴承恩写《西游记》是要通过描写天堂、人间和地狱的不同来揭示社会不同阶层的不同生活。天堂的美好自在，人间的世情险恶，地狱的冤孽惨烈，三相比较之中，小说家所要告诉读者的意思才能显明。也许完全是巧合，西方的诗人也喜欢通过对地狱、人间和天堂的描写揭示人类社会的阶层内容。我读但丁的《神曲》，首先想到的就是吴承恩的《西游记》。 ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> ) <br />
&nbsp; &nbsp; 也许人类社会真有过原始时期的共产主义。但从猴群生活中观察，猴王的自在享受实是源于许多能力不济的猴子的屈辱困乏。天地赐予人类的不可谓不丰富，但人类享用天地所赐之物却必须付去种种的代价，最起码需要体力脑力的劳作。但人类之中却总有一部分人只愿意享受不愿意劳作，而人类历史的实际正是一部分人不劳作而得到享受，甚至不劳作而得到大享受，得有大自在。不需劳作而能有大自在、大享受，这就是天堂的生活。天堂的生活令人陶醉，令人留恋，令人向往，令人追求。在中国民间的传说中，除了少数思春的女子，天堂里的人是很少愿意下到人间的。在吴承恩的笔下，连思春而下凡的女子难以见到，除了被派遣（擒妖魔或设障碍）和受贬黜，天界人物是很少有下到人间的。但人间的人物甚至畜生却总是心想着上天，到天堂中享受。猪八戒是从天上贬下人间来的，所以他天天怨天尤人，骂骂咧咧。孙悟空是从人间上闯过天宫的，所以他特别地自高自傲，自擂自吹。从这两个人物的表现可以看出，从天堂下到人间是多么不可忍受，而能从人间升入天堂是多么让人荣耀。 ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> ) <br />
&nbsp; &nbsp; 不能上天堂是绝大部分人自己明白的事。于是，人们就致力于不堕入地狱。行善积德固然是不堕地狱的途径，恃仗强力时常也能奏效，甚至可能取得更好的效果。孙悟空大打出手，逼迫判官、阎君删去生死薄上的猴儿们的名姓，使得猴儿们得以永世长生，正是吴承恩生花妙笔创设的例证。而人们为什么不愿意堕入地狱，读一读《西游记》中有关唐皇游地狱的内容，我们就可明白。 ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> ) <br />
&nbsp; &nbsp; 只有明白地狱的存在，我们才能明白人间的价值，我们才能明白天堂的美好。假如没有地狱，人人会争入天堂。假如人人入得天堂，天堂就不再是天堂，天堂就只似人间，甚至天堂会变成地狱。其实，假如我们反过来思考，没有天堂就很可能没有地狱。之所以需要一个地狱，就是因为有些人需要一个天堂。原因就是那点简单的道理：不愿意劳作又特别想得到大享受、大自在的人们需要剥夺他人劳作的果实以为自己的供养。剥夺很可能遭遇反抗，地狱就是处置反抗者的所在。受剥夺而不反抗者应可呆在人间，但被剥夺到无以自活者，实际上亦入了地狱。天堂是人造的，地狱亦是人造的，社会之所以分为阶层，都是人造的结果。 ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> ) <br />
&nbsp; &nbsp; 正因为社会阶层是人造的，所以有人就想到可以通过人的力量，拆毁社会的阶层。从马克思到毛泽东，所谓以阶级斗争方式达到消灭阶级的目的，实际上就是要通过地狱和人间对天堂的造反，达到拆毁天堂和地狱的目的。 ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> ) <br />
&nbsp; &nbsp; 孙悟空依仗自己的强力，使自己的子孙不复堕入地狱，他还要依仗自己的强力，使自己和子孙进入天堂。古今的许多造反者、革命者有同样的思路。孙悟空不想拆毁天堂，只想天堂容纳自己，当天堂的主人不愿接纳他或仅仅只是让他栖身，他愤怒之下要赶走天堂的主人，也只是要他自己做天堂之主，由他自己制定天堂的规矩。所谓“皇帝轮流做，明年到我家”，道尽了多少造反者、革命者的心思。 ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> ) <br />
&nbsp; &nbsp; 蒋介石革命成功，其亲族友朋皆成贵族，所谓四大家族，蒋宋孔陈，无一不是蒋中正之亲友。革命不成，造反失败，革命者、造反者皆入地狱；革命胜利，造反成功，革命者、造反者俱进天堂。这样的历史逻辑，在人类社会演绎了千万年。所以，共产党革命胜利成功，胜利者、成功者进入天堂，从历史逻辑推演，顺理成章。正是在这一点上，毛泽东违逆了历史的逻辑，在把地狱里农民和工人解救出来的同时，他阻止胜利的共产党人进入天堂。他革命成功后做的最主要的工作，实际上是领导全国人民毁掉天堂。尽管他在理论上把人间的社会主义、共产主义说成是人间的天堂，实际上，他的全部努力只有一个目标，就是在人间彻底地毁灭地狱和天堂，而全心全意地建设理想的人间，用他的话表达，就是全心全意为人民服务。 ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> ) <br />
&nbsp; &nbsp; 人人自食其力，不允许不劳而获，这就是不允许有人享受天堂的生活。消灭剥削、消灭差别，这就是不让人再过地狱的生活。但世界上总是有许多人想过不劳而获的生活，尤其是当他拥有了强力、强权或其它的自认为可依凭着不劳而获的资本。这类资本的拥有者最显著的就是革命的胜利者、成功者们。于是，毁灭天堂与守护（或新构）天堂的斗争无可避免。共和国历史上这场斗争的轰轰烈烈已有许多或歪曲或如实的描述。其大概，是毛泽东在世时，天堂和地狱大致消失（尚未毁尽），尽管许多人以为自己堕入了地狱，但这应该是因其久居天堂而乍入人间误以为人间即是地狱；毛泽东辞世后，天堂和地狱重新构建。 ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> ) <br />
&nbsp; &nbsp; 重新构建天堂是受毛泽东所阻而未进天堂或已进天堂而被毛泽东拉出者们的自然选择。天堂之所以需重构，是因为天堂已几乎被毁且原有的天堂并不够理想。地狱之需要重构，是因为原有农民和工人挣出了地狱，原地狱几乎被毁，但无地狱则无法维护天堂。 ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> ) <br />
&nbsp; &nbsp; 今天的天堂里人员越来越多，且以权势拥有者和资本拥有者及其子孙为主。今天的地狱里人员也越来越多，多是无工职之工人、失农地之农民。当然，活在人间的人依然是大多数，他们愿受天堂里人的剥夺而又暂时经得住剥夺，他们或担心堕入地狱而勤慎劳作，或想进天堂而积极奋斗。 ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> ) <br />
&nbsp; &nbsp; 前不久，奥巴马访问澳大利亚前，在白宫接受澳大利亚电视采访时，针对中国的迅速发展，说出了一番令许多中国人听起来特别会愤慨的话。奥巴马说：如果十多亿中国人也过上与美国人和澳大利亚人同样的生活，那将是人类的悲剧和灾难，地球根本承受不了，全世界将陷入非常悲惨的境地。美国并不想限制中国的发展，但中国在发展时要承担起国际上的责任。中国人要富裕是可以的，但中国领导人应该想出一个新模式，不要让地球无法承担。 ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> ) <br />
&nbsp; &nbsp; 奥巴马这个半黑半白的美国人，被许多人认为是美国的首位黑人总统。他应该对美国历史上白人之所以富有、黑人之所以困顿有过思考，他也应该对今天美国的白人、黑人都比较富有而别国的人民普遍贫困有过思考。他的这番话实际上是舍弃了虚伪的实在话。历史上的美国，之所以白人吃肉黑人吃糠，是因为只有黑人吃糠才能保障白人吃肉。现在的世界上，美国人之所以能吃肉，实际上就是因为别国的人民只能被迫吃糠。美国人之所以能生活于天堂，就是因为有许多国家的人民生活于地狱。其实，中国的许多过着天堂生活人也能够明白，他们之所以能在中国过天堂的生活，就是因为更多的中国人过着地狱的生活。美国人的天堂生活建立于世界的普遍地狱化，中国部分人的天堂生活则建立于中国的普遍地狱化。奥巴马实话实说应该有其目的，他是要中国的决策者控制住天堂里的人数，不要让十多亿中国人都享受天堂生活，不然，不仅美国人不能安享天堂生活，中国的部分人也不能安享天堂生活。 ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> ) <br />
&nbsp; &nbsp; 天堂生活只能部分人享受，其他人只能成为天堂生活的供给者和维护者。美国人明白这个道理，中国人也明白这个道理。所以，奥巴马要在这方面教训中国人就显得没有道理。 ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> ) <br />
&nbsp; &nbsp; 基于以上的分析，我们就应该明白，仇恨毛泽东的人就是那些想过天堂生活的人，尤其是那些已经过上了天堂生活又被毛泽东拉出天堂的人。人类社会之中，一般的人首先想的是不堕入地狱，然后想的是有可能升入天堂。所以，想脱离地狱的人，已被毛泽东拉出地狱的人，或不想再堕入地狱的人，还有那些虽离地狱较远但衷心不愿人类社会存在天堂和地狱的人，会感谢毛泽东，支持毛泽东，崇拜毛泽东；而自思不可能堕入地狱，只想着升入天堂或已入天堂的人都或多或少会忌惮毛泽东，诋毁毛泽东，仇恨毛泽东。 ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> )<br />
