													<h2>解释关于为什么绝对不能与中共妥协</h2>
						
						
																											<div id="postmessage_473743" class="t_msgfont">很久没上Twitter，前几天上的时候偶尔发现了一些推友居然把一些很可爱的想法说了出来：<br />
<br />
&quot;@lianyue @cxzj 我能够理解那种不对共产党抱任何希望否定共产党改良丝毫可能性的想法，但是，当你把共产党从中国的民主化进程中完全排除的同时，你的解决方案是什么？是中国经济崩溃各地暴动军队哗变？还是某一天上帝发一道雷电把共产党员都劈死？ &quot;<br />
<br />
我不得不承认当看到说不能把共产党从中国的民主化进程中完全排除这样的说法时，我确实被雷倒了。我觉得有这些想法的人九年义务教育应该学得很不错，大脑终于被养成了世界上仅有的两种逻辑方式之一的中国式逻辑。在这些人的脑海里浮现的大概是一个怎么样的思路呢？我个人猜想应该是“民主应该能有效包容各种各样的政治力量，中国共产党也算是一股政治力量，所以，它也应该被包含进来，参与到中国的民主化进程之中。而且，中共作为一股中国最大的政治力量，脱离中共单独搞民主是不现实的，应该走一条符合中国国情的渐进式的民主化道路。”看起来似乎还算比较合理，但是，这里面却隐藏了一个藏得很深的逻辑漏洞。“中国的民主化进程”指的是一个追求民主的过程，而“民主应该能有效包容各种各样的政治力量”这样的想法的前提是这个社会是一个“民主社会”，也就是说，要使这句话是有意义的，你所处的这个社会必须已经建成了民主社会，但是如果还处于追求民主的过程中，那怎么会是“已经是民主社会”呢？若既然已经是，那还用得着去“追求”么？这么一分析，某些人的想法就变得很荒唐了，因为他们把结果后面的事强行套进了追求这个结果的过程中，这样就导致了逻辑错误的产生。<br />
<br />
而对一些提出反驳意见的另一些童鞋，这位童鞋还说了一句很可爱的话“ @cxzj: 今天想不能这个逻辑没关系，以后慢慢你就会想通的。 ”，这其实又犯了另一个逻辑错误，因为cxzj这位童鞋提出的是一个想法，既然是想法，那就不是现实，就是还没有经过验证的东西，一样东西都没被验证过，那就是说它的正确与否是尚未得知的，但是从人脑的机能来讲，它会很主观，甚至有时候会很“自私”地在没任何证据支持下认为它自己想的东西是对的，并在潜意识默认自己是对的情况下迫使别人去相信自己的想法，例如“以后慢慢你就会想通的”这句话的前提首先是你这东西必须先得是“通”的，你才可以要求别人以后去“想得通”，但是，这条路到底通还是不通，我觉得除了上帝或个别有所谓的预知未来的能力的人士才有可能知道，你根本都没经历过并亲眼目睹过你所假设的东西演化出的最终结果是通还是不通，你怎么要求别人“去想得通”？。去把一样尚未知道结果的东西描绘出来并要求他人去相信这些东西在“以后”是可以实现的，这不正是中共这么多年年来辛辛苦苦画大饼的那一套么？<br />
<br />
<br />
能把这些本质上根本狗屁不通的东西想到通是很不容易的，不过这些荒诞的想法并不是某个人的专利，整个中国有这些想法的大有人在。没办法，谁让神经系统逻辑分析这类东西向来都那么冷门……<br />
<br />
<br />
<br />
从“谈判”这个词在现实中的具体表现来说，能够坐下来谈判的双方手里必须都要有对方所忌惮的筹码，我们将来如果打算用谈判和对话的方法跟中共妥协而达成中国的民主，请问我们手里的筹码是什么？就凭借着整天在Twitter上歪歪唧唧就能让中共忌惮？真可爱。中共现在代表的是中国历史上这么多年一直延续下来的一种野蛮封建专制的力量，中共如果还有一丝余息，它绝对不会谈判，而且绝对会把那些威胁到它专制统治的人（亦即现在在看帖子的你）剿灭到底，而当它希望能坐下来谈判的时候，证明它已经是强弩之末，它对局势的发展已经完全失去了掌控的能力，这样一来，中国的民主进程有没有它参与都是无足轻重的事了。又何必再多费口水。所以，无论如何，同中共进行所谓的谈判或期盼双方相互妥协都是一种很不切实际的想法。民主是怎么来的？民主本身就是靠把这种封建专制消灭之后才会有的。中国的政治环境在有中共参与的情况下是绝对无法完成民主的，如同没有人会把小偷雇来看守金库，也没有人会把一个恋童癖雇来参与到照顾你家的孩子的过程中，因为，是狗，它就改不了吃屎。<br />
<br />
民主包容一样东西指的是在它本身的范围内部的包容，而不是去包容它外部一种根本与它对立互斥的东西。专制社会中的民主力量必须是在推翻专制之后才能够开始包容，而不是在追求民主的过程中错误地理解包容这个词来纵容专制。而在这之前，对付专制，是无法跟它讲民主的。<br />
<br />
每一样东西都有消灭它对立面的趋势，民主作为一种意识形态，自然也不例外，封建专制和民主不存在共存的可能，如同你找不到一张黑色的白纸，同时，不要举君主立宪这个例子来证明你在理解问题本质方面的无知。那不是一种妥协，而是要求民主的那个阶级在占有大部分社会资源和社会权力下迫使专制的屈服。人类有史以来能实现民主的国家要么是通过暴力推翻封建或者在掌握大部分社会资源的情况下迫使原有的专制体制屈服，试问在现在这种情况下，有人能通过努力获取绝大部分社会资源么？根本都不开放给你，获个鸟毛啊，所以，只剩下一条路可以走，那就是完完全全地把中共这个名词所代表的这个实体彻底地从中国这块地方消灭掉。因为现在的中共本质来讲不过一股是打着名为中共旗号实行封建专制的特殊利益集团，他们根本就不介意再换个旗号，相反，一旦通过和平的方式达成了所谓的民主，中国的经济命脉，新闻媒体和绝大部分的社会资源还是掌握在那一批人或那一批人的后代手中，他们完全可以凭借着手中掌握的资源来操纵和影响选举结果，反而使得他们能比现在更名正言顺地剥削整个国家和社会。从而导致一种彻彻底底的假民主，那样的话，民主又有何意义？为了防止这种情况的出现，现在这一批人必须被消灭，他们手中掌握的东西才能重新释放回到社会，我们将来才有足够的资源去建设真正的民主制度。<br />
<br />
很多人或许觉得通过暴力来实现政权的更迭意味着流血，意味着死亡，但很不幸，自从我们这个物种在这个星球上存在以来，如果一个社会没有完成民主化的任务，那么在实现民主之前的政治就意味着权力之间血肉模糊的交织与斗争，意味着铁与火的熔炼，如果你只想当一个无聊时候发两句牢骚的旁观者，尽管去意淫和平之路，但如果你决定去趟政治这趟浑水，去打算用你脑海里那些的美好东西来取代现实中这个丑陋的怪物，这，就是游戏规则。<br />
<br />
如果上面一段描述让你觉得很不舒服的话，也不必过于介怀，我想说，“暴力”这个词其实并非就如同大家所想的那么暴力，我更倾向于用它来描述一种物体取代另一种物体的趋势的方式，就是说，指的是两种物体并不是以一种和平的方式去完成交替的，而是带有一些斗争的味道。当然，社会的发展或许也会使得这个取代的趋势不一定那么血腥，在这场斗争中，追求民主和追求维护专制独裁统治的双方作为主要选手，可是，我们似乎遗忘了社会中的另一股力量――军队。有些人觉得“指望军队暴动”是很可笑的，但恰恰相反，在军队倒戈支持下推翻现行政府是中国未来推翻专制统治的各种方案中成本最少，最能减少流血冲突的方案。<br />
<br />
中共为了应付越来越紧迫的外部威胁，必然要大力发展高科技的军备，而高科技的军备是无法由文盲来操纵的。一个人整体文化素养的提高，很大程度上会具备初步的思考问题的能力，有思考能力的人就是中共最大的威胁，因为其能够多年统治靠的就是利用谎言和扭曲事实不断地愚民和洗脑。同时从马斯洛的需求理论上来讲，这样的人会更关注自我方面的需求而不是集体方面的需求，这样就令中共原本那一套洗脑方法的效果大打折扣。当然，马斯洛的理论在中国失效的一个群体是中国的既得利益群体 ，用它无法来解释那一群人在极度满足底层需要之后为何需求仍然继续停留在底层，这也决定了以后军队倒戈的力量只能是那些最基层的青年士兵，因为中国的资源并不足以完全满足他们每一个人使他们都成为既得利益者，而很多军官虽然具有不低的文化素养，但他们已经成为既得利益者，他们很大程度上要维护自己的利益而继续服从中共。<br />
<br />
为了防止出现这样的局面，中共只有三种选择，一种是征集大量低文化水平的人进军队，另一种是加大力气去进行洗脑活动，第三种，靠给予大量的物质方面的东西来拉拢住军队为其卖命。第一种措施在周边科技含量不断提高的军事力量的包围下无异于自杀；对于第二种洗脑活动，只要是稍微接触过这个物质社会的人，基本都对那些冠冕堂皇的说教语言有一定免疫力了，而中共在未来又不能大力去宣传过去那一套人民军队理论，因为要维护其统治，将来的军队必然要是一支在必要时能够听从特殊利益集团的驱使去屠戮人民的军队，思想工作这条路走不通了，决定中共未来只能以物质方面来拉拢军队，但是一个国家的资源和经济是有限的，钱永远只能落在很少的一部分人手中，不惜一切代价增加国防军费稳住军事机器只能使整个国家的经济和货币加速崩溃，使中共死得更早，同时，在军费一定的情况下，人员待遇方面的增加会削弱武器研发的投入，就会回到如何解决外部军事压力这个难题上，伊拉克的军队在美国军事攻势面前的表现是有目共睹的，如果现在是地理大发现之前的中国，中共完全可以供养起一支低素质的打手式的军队来维护其统治，但是它现在面对的已经不仅仅是它自己的民众，还有周边的军事势力的威胁，这两方面对其政权的威胁使得对军队的作战要求是不同甚至从某些角度来讲是矛盾的。抓住这一重要问题作为突破口，就会使中共很无奈，甚至很郁闷，因为它在任何一方面做的努力都会削弱它在另一方面的努力，最使它郁闷的是，平衡点是不存在的，因为它要应付的这两样矛盾都不是静态的矛盾，这两样威胁都只会越来越大，为了不被消灭，它不能原地不动，它必须作出努力，而努力又会削弱努力……抓住从思想基础来动摇军队这个要点这就可以把中共拖进一个死循环，一个足以绞死中共的莫比乌斯环。正因为中共解不开这个环，所以根本不怕中共知道这个策略，它为解开这个死循环做的每一点努力都会使它离死亡的深渊更近……同时，能成功争取到武力方面的支持就顺便可以把我们在争取民主的过程中的另一个大难题一起解决掉，那就是如何面对中共的暴力镇压的问题。毕竟，中共之所以为中共，就是因为在维护其统治的本质问题上毫不动摇，只要有人会威胁到它统治的根基，它根本就不会顾及所谓的狗屁舆论压力用暴力机器来屠戮你，你赤手空拳，又能怎么样？<br />
<br />
当然，以上只是一个思路，一个非常粗糙的框架，具体实施起来还有很多要完善的地方，要协调调度各种各样的东西，这已经不是我一个人力所能及的了。<br />
<br />
至于那什么7000万中共党员的问题，根本就不是什么问题，我已经一再说过，他们早就不是所谓的中共，只不过他们的名字叫这个名字而已。这些人99.99%是没有信仰的，他们眼中只有自己的利益，中共党员这个身份不过是为方便他们自己谋求利益，他们不会为了他们章程上所写的那些东西去牺牲，就这么简单。<br />
<br />
<br />
或许某些人觉得一步一步慢慢地促使整个社会的改变达到民主比采用激进的方法好，他们感性地认为过程虽然缓慢，但好像不会死人，但是，我不知道他们有没有想过，这个专制政权以及这个专制政权主导下的畸形政府机构体制每存在多一天，本身就会导致更多人死于或“被”死于各种各样的人祸！死在你们的所谓不发生大规模暴乱的“和平时代”！难道在那些号称尊重生命的人士的眼里，只有死在追求民主过程中的那些才算是生命？而现在每天惨死掉的那些的矿工，小贩，最无辜的小孩子，那些最普通的中国人，他们就不是有生命的？<br />
<br />
我知道你们可能很有耐心，相信时间可以改变一切，但你们眼中最富余的“时间”现在已经是一件无比昂贵的奢侈品。当全体人民都觉醒那一天无疑是实行民主最好的时机，但我们等不到了，中国这块土地上的资源和环境承载能力已经很快就要达到极限，中共的官员靠着污染和盘剥这块我们世世代代生存的土地为自己获取大量的金钱然后可以出到外国继续过他们舒适的生活，说白了，现在的中国不过是那些特殊利益集团为使自己能获得更多利益能去到国外享受一个优良得多的生活环境而可以不顾后果压榨的有机体，一个用完之后可以丢弃的跳板。当中国这块土地被压榨完，没有利用价值的时候，他们就根本不稀罕了，反正他们的后代能在国外喝牛奶，吃面包了。这块土地上的人不是要民主吗？给你吧，反正民主或专制对他们来说不过是两个抽象名词，他们本来就不是一群因为他们信仰而打压我们的人。不要说什么只要可以改变，我们就有耐心等一百年，等一千年之类的鬼话，十年我们也等不起。如果再过多十几年也没什么事发生的话，大家也不要争取什么什么争来争去了，“民主”不单单是人民主权，这个词本身还有一个潜台词，那就是这个地方是有人存在的，有人存在就意味了要有能支撑人类生存和发展的必要自然条件和资源。当一个地方变成一块不适合人类生存的不毛之地，即使愚蠢如中共之流，也不会同你争的。<br />
<br />
是不是觉得有点悲观？如果觉得起义革命希望不大，又不想留在中国等死，欧美又没钱去，干脆找片无政府的区域如什么非洲之类的，建立一个中国人主导的民主国家算了，就好比当年坐五月花号去美洲那些家伙一样……<br />
