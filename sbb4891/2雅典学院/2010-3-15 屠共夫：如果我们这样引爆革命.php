<br />
中国现在的形势，如各位所说，的确是需要我们出来作点事情的时候了。共产党的残暴野蛮，反文明反人类罪行，正在共匪头子胡锦涛这个恶霸左棍日益严密的控制和绞杀之下，滑到灾难的境地，毛泽东是恶魔，他就是魔孙。有人说胡锦涛只是出于谨慎，所以大力维稳。问题是，共产党是毛泽东以暴力恐怖加流氓欺骗建立的一个黑帮集团，其内部已经必然性地演变出大量的黑势力，他们出于利益权势的贪婪，对于稳定具有强烈的要求。因此，共匪头目的任何一点维稳动机，都会在下面的共匪集团内部放大为千百倍强大邪恶的反人类力量，胡匪作为共党头目，其僵化愚昧的思想对于罪恶的推动，是罪大恶极的。在全面的铁一般的稳定之中，共产党已经完全黑化，成为民族生命巨毒，而人民则在社会的整体道德崩溃和猖獗的黑帮官僚压榨吞噬下，陷入奄奄一息的绝望地步。股市是黑帮，房地产是黑帮，公安是黑帮，无处不是黑帮，郎咸平城之为“黑领阶层”，在无处不在的黑领阶层的扩张下，普通百姓沦为房奴和蚁族的，源源不断。人们对于他们自身的命运，觉悟的越来越多，不彻底灭绝中共魔兽集团，就永无希望。现在光靠磨嘴皮子，一点用都没有，刘晓波即使靠耍笔杆子弄个诺贝尔奖，也只是轻如鸿毛，对于推翻共产党，对于为人民报仇，对于民主在中国的实现，有什么价值呢？共产党是死心塌地的流氓恶棍，唯有行动起来，作点事情，才是千古不可磨灭的。<br />
<br />
黑帮极权社会的腐朽，即使是靠着黑帮组织黑帮机构的大力控制，也只能维持表面的稳固，民心已经在渴望剧变，仇恨和怒火在地下燃烧很久，中国的社会内部，孕育成熟着大暴乱，大崩裂，大复仇，大屠杀，大革命。渴望剧变和复仇的民众正在观望，正在等待时机。只要时机发作，人民就会行动，他们不仅会从乡镇，从城市起来，而且还会从共匪的旧军队中起来，一起来，大复仇就会闪电一般蔓延，现有的共匪官僚黑帮机构中百分之九十九的人，都会在数日之内人头落地，因为他们依靠现代武器进行的控制和镇压已经超越了中国历史，因此他们积攒的血债和仇恨，也已经超越了中国历史上任何一个封建王朝。只要在日常的生活中留心一点，就会知道几乎没有一个老百姓不对共产党暗中恨的咬牙切齿。所以，关键是要以坚定的行动，推动产生这么一些时机，引爆中国大地上巨大的火药桶，彻底炸毁共产党的魔国。<br />
<br />
局势就象下棋，共匪现在控制局势的方式，是消灭反对派任何有威胁性的招式，不放过一点漏洞。但好在，棋局是无限的，不可能有全盘通吃者，俗话说，“世事如棋局局新”，因为人是有智慧的，灵活的。我们在棋盘上，总可以下出那出其不意的一招，打破对手的控制，让他不得不乱，不得不变，不得不应对。我们现在就是需要这么出其不意的一招，棋子虽小，一步见效，一步调动全局。<br />
<br />
决心和武器是第一位的要素。只要有20或者30个人，甚至只要有10个人，象杨佳和邓玉娇一样有不怕死的决心和愤怒意志，就足够了。这样一个抱赴死决心的小组，严密行动，在现在黑社会猖獗，唯金钱是图的情势下，即使比较费力，也可以搞到足够的弹药，弄到轻型机枪和制备好强力爆炸器材。如果每个人有一只冲锋枪，再配备十几枚手雷，配备重型炸药，那么，就完全可以端掉一个省级城市的共匪政府机构，即使不能端掉省政府，也可以端掉地区级政府，至于端掉一个县政府，则更是不在话下。共匪控制虽然严密，但不可能严密到全中国，不可能严密到每个县政权，而且，长期以来，正是因为其严密，中共匪帮官僚都十分得意忘形，骑在人民头上作威作福，绝对想不到会有一支小分队突然从天而降。那些省政府，县政府，无不在粉饰的太平街景中醉生梦死。《黑客帝国》中Neo准备突袭特工老巢时，Trinity劝阻他说，“没有任何人敢这么作的”，Neo回答：“所以我们才会成功”。一个小分队，可以任意选个太平日子，利用现在的车辆作交通工具，灵活自如，悄然隐蔽地接近目标，然后突然行动，一阵冲锋即可解决哨兵门卫，然后向主建筑投掷手雷，封锁出口，同时安放重型炸药装置，比如烈性炸药卡车等。如果计划周密，十分钟内即可爆破主建筑。突然的混乱之中，甚至是可以从容撤退的。重庆抢枪勇士一案，是经典范例，出其不意，迅速按计划撤退，消失的无影无踪。<br />
<br />
一起这样的行动，就象在共匪的棋盘死穴上下了一手，就必定要大面积调动共匪的军队。而军队，则是武器，是人心，是机会。据我所知，共匪军队中现在是腐败横行，军心涣散，可以说没有什么实际的战斗力，愿意为共产党卖命的很少，很多普通士兵，都和社会上普通百姓一样，对共匪腐败恨之入骨，只是也和普通百姓一样，无奈忍受。而共匪对他们，除了强化洗脑，加强管制外，也没有任何根本的有效措施，实际上对士兵也不放心，也是实行枪弹分离，有武器等同没武器。如果我们有了这样的行动，就和预料一样，必定会大大调动军队，各个共匪政府都会驻扎军队，枪里面就必定会有子弹，有了子弹，军队中想行动的人，想豁命一拼的人，甚至单纯受了欺压和不平而想报仇的人，就有了机会，复仇行动就有了新的渠道。而这样一起行动，则必定大大鼓舞民心，加剧共匪内部的分化，使其强力的镇压和管制，变得力不从心，天下大乱的剧变时代，就回接踵而至。这样的行动起示范作用，使大量的复仇者，不再犹豫，而是实际地起来，以积极的创造，加入到灭共大潮之中去。人民的热情是不可低估的，尤其是深怀仇恨的人，拆迁的，上访的，都会停下那些毫无意义的投诉上访甚至懦弱无骨的自杀自焚，转而谋求实际的拼命。地下的人民复仇组织，就会在秘密的联合之中产生，更大规模的全面的复仇灭共行动，就必然会形成。我们需要行动，类似的小型大行动，不一定是捣毁共党政府大楼，可以是任何具有重大影响的行动，捣毁武警或驻军司令部之类也可以考虑。<br />
