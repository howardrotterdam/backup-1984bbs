													<h2>回复 2楼 北京棋迷 的话题</h2>
						
						
																											<div id="postmessage_565492" class="t_msgfont">首先说一下的是：当年苏波战争，波兰一方是要”收复失地“，苏联一方是要”继续革命“（托洛斯基语），跟德国是没关系的，德国当时是亲苏联的。把德国扯进来，显然是要跟二战发生联系，利用德国人的恶名来丑化波兰人，但却完全是造谣。<br />
波兰这个”收复失地“有道理没？平心而论，波兰要收复的地方，虽然历史上曾经是波兰的，但一般都是混居区，部分地区搞不好俄国人还是多数；但是反过来，也可以这么说：但凡俄国和别国发生领土纠纷，一般都是俄国的错。<br />
要打仗，当然难免要有暴行。波兰人有，红军岂能没有？光统计一边的，不统计一下红军对当地波兰地主的暴行（似乎波兰人多为地主，但这个是苏联人的一面之词，我不能肯定），怎么行呢？<br />
维基百科上说：1920年，波兰在协约国和罗马教廷支持下的向东扩张，红军在华沙战役中的完败。1921年3月18日缔结的里加条约让波兰获得了西白俄罗斯和四分之一个乌克兰。作为这场战争的牺牲品，12-13万苏维埃战俘于1919-1921年间被关押在波兰，其中的65000到70000回到了俄国，超过6000人成为了白卫军（在波兰、德国、罗马尼亚和波罗的海国家），2000人成为波兰居民并加入国籍。在 1920-1921年间，45000名被俘的红军战士在波兰被关押期间死亡。<br />
这个死亡比例看起来很大，但是比斯大林格勒战役中被俘德军死亡90%以上的情况来说，也不算什么。<br />
好吧，在此基础上，卡廷事件算是报复吗？<br />
我只能说：俄国人要报复，那总是没办法的。苏联红军四处奸淫掳掠，都可以算报复：在中国东北，是报复日本人，在东欧，是报复德国人（实际上东欧人不喜欢苏联人的平均程度要高于不喜欢德国人，匈牙利、罗马尼亚、保加利亚、克罗地亚等等都是德盟国，所以“活该”被报复）。不讲理的话，就可以随便报复任何人。就像那个拿菜刀捅了小学生的人一样，那也是报复社会。<br />
问题是：<br />
第一、作为中国人，没必要原谅俄国人。<br />
第二、苏波战争，苏联都是罪魁祸首，因为它的根源在于帝俄对波兰的统治，波兰从帝俄中独立出来，难免要发生边界纠纷。布尔什维克搞革命推翻帝俄和临时政府的时候（对，前一个革命也有布尔什维克的份儿），就种下了这场战争。遑论苏联在二战时的侵略了。</div>

							
							
							
							
