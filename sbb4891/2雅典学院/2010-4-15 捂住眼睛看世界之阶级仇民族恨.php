													<h2>捂住眼睛看世界之阶级仇民族恨</h2>
						
						
																											<div id="postmessage_565459" class="t_msgfont">捂住眼睛看世界之阶级仇民族恨<br />
花大熊 @ 2010-4-14 21:56 阅读(103) 评论(5) 推荐值(0) 引用通告 分类: 未归类<br />
<br />
&nbsp; &nbsp; 波兰总统在俄国挂了，政府吊唁行礼如仪，民间却不乏调侃叫好声。<br />
&nbsp; &nbsp; 为什么？<br />
&nbsp; &nbsp; 因为这位总统大人跟达赖是好朋友，带头抵制北京奥运会，是反华反共的急先锋，我们不喜欢他。<br />
&nbsp; &nbsp; 说到底，我们记仇！<br />
&nbsp; &nbsp; 记仇好不好？我不知道，这有好几种说法。但是有句话叫做“忘记过去就意味着背叛”！这是记仇的人才说得出的话。<br />
&nbsp; &nbsp; 总统带着军队高官，带着专门记忆过去的团队和当时的遗老，跑到卡廷森林这个地方悼念自己国家被杀的军官，他是去化解仇恨，消除记忆的呢？还是去让这些现在掌兵掌权的人记住某些东西的呢？<br />
<br />
&nbsp; &nbsp; 是不是一定应该记忆和煽动仇恨？那倒不一定，要看仇恨的根源。<br />
&nbsp; &nbsp; 苏联人为什么要杀两万多放下武器的波兰军官呢？难道他们真的是杀人狂魔？杀两万多人就图一个快乐？没有缘故吗？<br />
&nbsp; &nbsp; “世界上没有无缘无故的爱，也没有无缘无故的恨”！<br />
&nbsp; &nbsp; 原来波兰历史上就是地主资产阶级的的反共急先锋。就在这个卡廷惨案发生的十多年前，法国英国出钱，波兰德国出兵，要“把红色政权扼杀在摇篮里”，不但攻进苏联，占领大片国土，还抓了几十万苏联红军战俘，屠杀其中的共产党员、红军军官、骨干士兵。杀了多少人呢？苏联说是十万，又一说是七万，波兰人自己承认是一万多。<br />
&nbsp; &nbsp; 波兰军官团肯定没想到过十年自己会落到苏联红军手里。<br />
&nbsp; &nbsp; 这是无产阶级和资产阶级的仇恨，也是俄波两国的民族矛盾，是阶级仇、民族恨。<br />
&nbsp; &nbsp; 所谓的卡廷惨案里，苏联红军并没有不分青红皂白的杀战俘。人家当年优先杀共产党员和军官，苏联红军也照章办理，只杀旧贵族和军官，那都是些手上沾满共产党员鲜血的人。<br />
&nbsp; &nbsp; 有什么不对吗？<br />
&nbsp; &nbsp; “冤冤相报何时了啊……”（基督教还有好些话，可惜我背不过。）<br />
&nbsp; &nbsp; 去拥抱你的敌人吧，他们也会忘掉仇怨拥抱你的。<br />
&nbsp; &nbsp; 和解了吧，和解了吧，记仇不是什么好事。<br />
&nbsp; &nbsp; 如果你拥抱卡廷事件被杀的波兰军官，你也可以去拥抱拿战俘做实验那个731部队的石井长官，他一定会称赞你有度量的。<br />
&nbsp; &nbsp; 返回中国，如果解放军进军西北击败二马军队的时候，也许会说：“你们当年杀红军西路军也是各为其主，尽管我们今天占了上风，咱们还是做好朋友吧。”马步芳马鸿逵一定会很感谢解放军的宽厚。<br />
&nbsp; &nbsp; 当年还乡团抓住刘胡兰，一定是想跟她和解来着，可惜这十六岁的姑娘不识相，活该被铡刀铡死。<br />
为了不伤害纯洁的心灵，我们把脑袋钻进沙里，捂住眼睛，只看那些自己喜欢的事物。我们代表我们被杀害的父辈，请求敌人的原谅，并约定共同忘记过去的一切。<br />
&nbsp; &nbsp; 可惜先放下武器的人，总是不能如愿。<br />
&nbsp; &nbsp; 血腥时代有血腥时代的逻辑。捂住眼睛看世界的一班人，可以庆幸自己不生在那个血腥的时代，可以忘记那个时代发生的一切，却没有权利指责那个时代的人。<br />
&nbsp; &nbsp; 也许更应该问问，是谁捂住了你的眼睛？<br />
&nbsp; &nbsp; 波兰总统先生显然没有忘记历史，他也不打算忘记，他带了这个团队，是让他们记住被杀的前辈，擦亮眼睛去的。这个团队主要是军官，他们从被杀的前辈墓前回去以后，是会擦亮他们的枪？还是会放下刀枪，拿起花束？<br />
&nbsp; &nbsp; 我猜，他们是不会弄错的，自作多情的，是我们。<br />
<br />
（这篇东西的起因，是我向老母亲解释波兰总统和卡廷惨案，老太太听了之后，说：“斯大林真是个记仇的人啊”。想了想又说：“当年还乡团杀我们，我们杀他们，谁都没客气……”）<br />
（本文大约违背了西西河“不宣扬仇恨”的宗旨，版主看着办吧。）<br />
&nbsp; &nbsp; 附带说一句，上世纪末以来，各个国家都是共产党纷纷为当年杀人道歉，没看见有人为当年杀害共产党人道过歉。<br />
