													<h2>中共老人提点后辈 以党史论刘晓波判决违法</h2>
						
						
																											<div id="postmessage_444028" class="t_msgfont">作者：丁小&nbsp; &nbsp; 文章来源：RFA<br />
<br />
北京学者刘晓波被以“煽动颠覆罪”重判，不但引起国内和国际社会的反响，连共产党内部老一辈的党员也从中共党史的角度提出质疑。自由亚洲电台特约记者丁小采访报道。<br />
<br />
前《人民日报》社长胡绩伟、前《新华社》副社长李普、《新华社》老记者戴煌、中国社科院荣誉学部委员何方，日前联署公开信,呼吁对刘晓波的定罪的合法性提出质疑，呼吁当局重新审查。本周加入联署的还有曾任前人大委员长万里秘书，著名“三农”专家DD吴象，独立中文笔会的网站周三公布了这几位八九十岁老人在呼吁信上签名时的照片。<br />
<br />
联署者之一、国际问题和中共党史专家何方就此接受本台采访时说：“最大的问题就是以言定罪是不应该的，因为《宪法》上保障言论自由。刘晓波他并没有颠覆政府的行动，按说联邦共和，即使是错误的，你不同意他讲，但是他有发言权，你不能因为他发个言就把他抓起来，反正不合理，我是不同意，我是反对的。定一年都不应该，何况定了十一年，我个人是这样认为的，所以就签了个名。（您看二审会改判么？）我看不保险，因为他定了以后，中国人有个脾气，这个弯儿转不过来，不好下台，上了台以后不容易下台。”<br />
<br />
由于九十多岁高龄的老人听力不好，接受电话采访并不容易，但他们还是非常希望发出自己的呼声。其中李普先生接受本台电话采访时说：“对刘晓波的判决，没有道理啊！刘晓波到底犯了那一条法？没有那么严重吧？还是这个问题，欲加之罪何患无辞！（判他这么重有什么影响呢？您认为会否令知识界更加不敢说话？）这个。。可能。恐怕就是为这个目的吧！”　<br />
<br />
尽管反对以言入罪，在他们的公开信中，并没有直接批评这一判决违反《宪法》中保障的言论自由、天赋人权，而是从中国党史的角度论证判决的不合法。其中写道：年轻的同志可能不知道，我们这些老同志都记得：“联邦共和国”的口号是早在中国共产党“二大”就提出来的，“七大”党章党纲又重申的正确口号。建国六十年后，如果北京法院的法官把赢得政权的正确口号，颠倒是非，错定为“煽动颠覆国家政权罪”的罪证，那么，把我党党章党纲、党的统战政策、民族政策置于何地？把党和国家的老一代领导人置于何地？<br />
<br />
曾任中国国务院发展研究中心研究员的姚监复，在为最新一期香港《动向》杂志一篇就刘晓波判决撰写的评论文章--《 北京法院法官先生：请认真学习《毛泽东思想》，与上述几位老人的呼吁不谋而合。<br />
<br />
