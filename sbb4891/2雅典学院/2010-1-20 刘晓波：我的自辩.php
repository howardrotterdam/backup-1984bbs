													<h2>刘晓波：我的自辩</h2>
						
						
																											<div id="postmessage_443094" class="t_msgfont">我的自辩<br />
刘晓波（2009年12月23日）<br />
&nbsp; &nbsp; 《起诉书》（京一分检刑诉[2009]247号）列举了六篇文章和《零八宪章》，并总中引述了三百三十多字据此指控我触犯了《刑法》第105条第2款之规定，犯有“煽动颠覆国家政权罪”，应当追究刑事责任。<br />
&nbsp; &nbsp; 对《起诉书》所列举事实，除了说我“在征集了三百余人的签名后”的事实陈述不准确之外，对其他的事实，我没有异议。那六篇文章是我写的，我参与了《零八宪章》，但我征集的签名只有70人左右，而不是三百多人，其他人的签名不是我征集的。至于据此指控我犯罪，我无法接受。在我失去自由的一年多时间里，面对预审警官、检察官和法官的询问，我一直坚持自己无罪。现在，我将从中国宪法中的有关规定、联合国的国际人权公约、我的政治改革主张、历史潮流等多方面为自己进行无罪辩护。<br />
&nbsp; &nbsp; 一、改革开放带来的重要成果之一，就是国人的人权意识的日益觉醒，民间维权的此起彼伏，推动中国政府在人权观念上的进步。2004年全国人大修宪，把“国家尊重和保障人权”写进了宪法，遂使人权保障成为依法治国的宪法原则。这些国家必须尊重和保障的人权，就是宪法第35条规定的诸项公民权利，言论自由便是基本人权之一。我的言论所表达的不同政见，是一个中国公民在行使宪法所赋予的言论自由权利，非但不能受到政府的限制和任意剥夺，反而必须得到国家的尊重和法律的保护。所以，起诉书对我的指控，侵犯了我作为中国公民的基本人权，违反了中国的根本大法，是典型的因言治罪，是古老的文字狱在当代中国的延续，理应受到道义的谴责和违宪追究。《刑法》第105条第二款也有违宪之嫌，应该提请全国人大对其进行合宪性审查。<br />
&nbsp; &nbsp; 二、《起诉书》根据所引的几段话就指控我“以造谣、诽谤等方式煽动颠覆国家政权，推翻社会主义制度”这是欲加之罪。因为“造谣”是捏造、编造虚假信息，中伤他人。“诽谤”是无中生有地诋毁他人的信誉与人格。二者涉及的都是事实的真假，涉及他人的名誉与利益。而我的言论皆为批评性的评论，是思想观点的表达，是价值判断而非事实判断，也没有对任何人造成伤害。所以，我的言论与造谣、诽谤风马牛不相及。换言之，批评不是造谣，反对更不是诽谤。<br />
&nbsp; &nbsp; 三、《起诉书》根据《零八宪章》的几段言论指控我诬蔑执政党，“试图煽动颠覆现政权”。这指控有断章取义之嫌，它完全无视《零八宪章》的整体表述，无视我所有的文章所表述的一贯观点。<br />
&nbsp; &nbsp; 首先，《零八宪章》指出的“人权灾难”都是发生在当代中国的事实，“反右”错划了五十多万右派，“大跃进”造成了上千万人的非自然死亡，“文革”造成国家的浩劫。“六・四”是血案，许多人死了，许多人被投入监狱。这些事实都是举世公认的“人权灾难”，确实为中国的发展带来危机，“束缚了中华民族的自身发展，制约了人类文明的进步。”至于取消一党垄断执政特权，不过是要求执政党进行还政于民的改革，最终建立“民有、民治、民享”的自由国家。<br />
&nbsp; &nbsp; 其次，《零八宪章》所申明的价值和所提出的政改主张，其长远目标是建成自由民主的联邦共和国，其改革措施是十九条，其改革方式是渐进的和平的方式。这是有感于现行的跛足改革的种种弊端，要求执政党变跛足为双足，即政治与经济同步并进的均衡改革。也就是从民间的角度推动官方尽快启动还政于民的改革，用自下而上的民间压力敦促政府进行自上而下的政治变革，从而形成官民互动的良性合作，以尽早实现国人的百年宪政之梦想。<br />
&nbsp; &nbsp; 再次，从1989年到2009年的二十年里，我所表达的中国政治改革的观点，一直是渐进、和平、有序、可控。我也一贯反对一步到位的激进改革，更反对暴力革命。这种渐进式改革主张，在我的《通过改变社会来改变政权》一文中有着明确的表述：通过致力于民间权利意识的觉醒、民间维权的扩张、民间自主性的上升、民间社会的发展，形成自下而上的压力，以推动自上而下的官方改革。事实上，中国三十年的改革实践证明，每一次具有制度创新性质的改革措施的出台和实施，其最根本的动力皆来自民间的自发改革，民间改革的认同性和影响逐渐扩大，迫使官方接受民间的创新尝试，从而变成自上而下的改革决策。<br />
&nbsp; &nbsp; 总之，渐进、和平、有序、可控，自下而上与自上而下的互动，是我关于中国政治改革的关键词。因为这种方式代价最小，效果最大。我知道政治变革的基本常识，有序、可控的社会变革必定优于无序、失控的变革。坏政府治下的秩序也优于无政府的天下大乱。所以，我反对独裁化或垄断化的执政方式，并不是“煽动颠覆现政权”。换言之，反对并不等于颠覆。<br />
&nbsp; &nbsp; 四、中国有“满招损、谦受益”的古训，西谚有“狂妄必遭天谴”的箴言。我知道自己的局限，所以，我也知道我的公开言论不可能十全十美或完全正确。特别是我的时评类文章，不严谨的论证，情绪化的宣泄，错误的表述，以偏盖全的结论……在所难免。但是，这些有局限性的言论，与犯罪毫无关系，不能作为治罪的依据。因为，言论自由之权利，不仅包括发表正确观点的权利，也包括发表错误言论的权利。正确的言论和多数的意见需要保护；不正确的言论和少数的意见，同样需要权利的保护。正所谓：我可以不赞成或反对你的观点，但我坚决捍卫你公开表达不同观点的权利，哪怕你所表达的观点是错误的，这，才是言论自由的精义。对此，中国古代传统中也有过经典的概括。我把这种概括称为二十四字箴言：知无不言，言无不尽；言者无罪，闻者足戒；有则改之，无则加勉。正因为这二十四字箴言道出了言论自由的要义，才能让每一代国人耳熟能详，流传至今。我认为，其中“言者无罪，闻者足戒”，完全可以作为当代国人对待批评意见的座右铭，更应该成为当权者对待不同政见的警示。<br />
&nbsp; &nbsp; 五、我无罪，因为对我的指控有违国际社会公认的人权准则。早在1948年，中国作为联合国的常任理事国就参与起草了《世界人权宣言》；五十年后的1998年，中国政府又向国际社会作出了签署联合国制定的两大国际人权公约的庄严承诺。其中《公民权利和政治权利国际公约》把言论自由列为最基本的普世人权，要求各国政府必须加以尊重和保障。中国作为联合国常任理事国，也作为联合国人权理事会的成员，有义务遵守联合国制定的人权公约，有责任饯行自己的承诺，也应该模范地执行联合国发布的人权保障条款。惟其如此，中国政府才能切实保障本国国民的人权，为推动国际人权事业做出自己的贡献，从而显示出一个大国的文明风范。<br />
&nbsp; &nbsp; 遗憾的是，中国政府并没有完全履行自己的义务和兑现自己的承诺，并没有把纸上的保证落实为现实的行动，有宪法而无宪政，有承诺而无兑现，仍然是中国政府在应对国际社会的批评时的常态。现在对我的指控就是最新的例证。显然，这样的因言治罪，与中国作为常任理事国和人权理事会的成员的身份相悖，有损于中国的政治形象和国家利益，无法在政治上取信于文明世界。<br />
&nbsp; &nbsp; 六、无论在中国还是在世界，无论是在古代还是现当代，因言治罪的文字狱都是反人道反人权的行为，有悖于大势所趋、人心所向的时代潮流。回顾中国历史，即使在家天下的帝制时代，从秦到清，文字狱的盛行，历来都是一个政权的执政污点，也是中华民族的耻辱。秦始皇有统一中国之功，但其“焚书坑儒”之暴政却遗臭万年。汉武帝雄才大略，但其阉割太史公司马迁之举则倍受病诟。清朝有“康乾盛世”，但其频繁的文字狱也只能留下骂名。相反，汉文帝在二千多年前就废除过因言治罪的“诬谤罪”，由此赢得了开朝仁君的美名和历代推崇的“文景之治”。<br />
&nbsp; &nbsp; 进入现代中国，中国共产党之所以由弱而强，最终战胜国民党，在根本上源自其“反独裁争自由”的道义力量。1949年前，中共的《新华日报》和《解放日报》经常发文抨击蒋家政权对言论自由的压制，为因言获罪的有识之士大声疾呼。毛泽东等中共领袖也多次论及言论自由及基本人权。但1949年后，从反右到文革，林昭被枪毙，张志新被割喉，言论自由在毛时代消失了，国家陷于万马齐喑的死寂。改革以来，执政党拨乱反正，对不同政见的容忍度有大幅度提高，社会的言论空间不断扩大，文字狱大幅度减少，但因言治罪的传统并没有完全灭绝。从四・五到六・四，从民主墙到零八宪章，因言治罪的案例时有发生。我此次获罪，不过是最近的文字狱而已。<br />
&nbsp; &nbsp; 二十一世纪的今天，言论自由早已成为多数国人的共识，文字狱却是千夫所指。从客观效果上看，防民之口甚于防川，监狱的高墙关不住自由的表达。一个政权不可能靠压抑不同政见来建立合法性，也不可能靠文字狱来达成长治久安。因为，笔杆子的问题只能诉诸笔杆子来解决，一旦动用枪杆子解决笔杆子的问题，只能造成人权灾难。只有从制度上根绝文字狱，宪法所规定的言论自由权利才能落实到每一位国民身上；只有当国民的言论自由权利得到制度化的现实保障，文字狱才会在中国大地上灭绝。<br />
&nbsp; &nbsp; 因言治罪，不符合中国宪法所确立的人权原则，违反了联合国发布的国际人权公约，有悖于普世道义与历史潮流。我为自己所做的无罪辩护，希望能够得到法庭的采纳，从而让此案的裁决在中国法治史上具有开先河的意义，经得起中国宪法之人权条款与国际人权公约的审查，也经得起道义的追问和历史的检验。<br />
