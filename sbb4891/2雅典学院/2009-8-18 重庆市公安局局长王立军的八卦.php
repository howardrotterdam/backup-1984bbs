　　第二天下午三点半，侦察员报告，说杨俊富可能已打探到消息，正准备出逃。王立军经请示后，决定提前行动。他率领警员直扑杨俊富栖身的银州大酒店，自己埋伏在一楼服务员休息室。随后，他让刘家铎等人将杨俊富诓下楼来。杨俊富刚把半个脑袋探进门里，王立军就猛虎扑食般地将其擒获了。王立军大喝一声：“你知道我是谁吗？”杨俊富一看，如梦方醒：“啊，你是王立军？那我值了。”王立军问：“为什么值呢？”杨俊富说：“死在你手下呀！”<br />
　　<br />
　　不料，当王立军赶去捕获何晶时，他已得知风声迅速逃逸了，一个月后，才潜回住地。王立军得到情报后，为防止走漏风声，不顾危险，只带了一名警员，赶到几十公里外何晶所开的奥林公司进行追捕。他一眼就认出了何晶的“标致”505轿车。而此时，何晶也发现了一辆陌生的车子开来，他预感不妙，钻进车子开足马力逃跑。王立军开车死死地咬住他，眼看就要追上了，忽地，一辆拖拉机挡住了王立军的车子，望着何晶的小车飞驰而去，王立军气得眼里直冒火。然而，真叫得道多助，何晶的车子突然出故障熄了火，王立军跳出车子追了上去。一边是“武林高手”，一边是孤胆英雄，短兵相接，必是一场你死我活的较量。面对何晶使出的十八般看家本领，王立军毫不畏惧，凭着机智勇猛，与何晶对打了20分钟，最后一记重拳，将何晶这个号称天下无敌手的凶犯击倒在地。3个月后，肖建军也被捉拿归案。而涉案的19名国家机关内部的蠹虫，也被一网打尽。<br />
　　<br />
　　被称为中国“打黑”史上的最大一次战役取得了决定性的胜利。那天，作为公安部英模报告团的一员，王立军正坐在北京人民大会堂的主席台上。而这天恰好是这些黑社会头目被依法枪决的日子。王立军多想亲耳听到那正义的枪声啊，所以他从主席台上溜了下来。在大会堂的门厅，他拨通了刑警支队副支队长计连科的电话。老计说：“王局长，你听着，现在毙到第四个了。”“啪！”一声清脆的枪响，从电话里传了过来，回荡在四周。<br />
　　<br />
　　<br />
　　<br />
　　他从额尔古纳河走来<br />
　　<br />
　　<br />
　　<br />
　　古老的柴河曲曲弯弯地流过辽北大地。望着逶迤而去的河流，王立军却时常遥念起内蒙草原，遥念起额尔古纳河。他正是从那儿走出来的，他是蒙古族人，他是成吉思汗的后代。<br />
　　<br />
　　蒙古族是个彪悍的民族，所以，很小的时候，王立军就参加了当地的“民族摔跤队”。他练得非常刻苦，不出多久，他的高强武艺便使人刮目相看。后来，他又被挑到了内蒙古少年拳击队。但是，王立军更为痴迷的却是绘画、书法和音乐。他从初中时就开始做起了画家梦，一望无际的草原、小路边盛开的萨日朗、长空盘旋的苍鹰，都在他的笔下栩栩生辉。老师推荐他报考美院，果然专业和文化考试他都名列前茅，但他万万没有想到，由于门庭太浅，竟被别人占去了本属他的名额。一个草原儿子的画家梦就此破灭了。但是，中国的警界却因此有了个不可多得的人才。<br />
　　<br />
　　后来，王立军参军了；再后来，他成了辽宁省铁法市的一名交通警察。那年休假时，他一个人自费去了北京，可他并没有游山玩水，而是每天伫立在长安街头，看那儿交警的指挥手势。他拍了许多照片，回到家后就自己练习，结果，那些司机和行人每当经过他的岗亭，都情不自禁地要向他行注目礼。一天，市长在上班的路上发现没有了王立军的身影，便问他上哪去了。有人告诉他，王立军已被调去当治安队长了。市长有些光火地说：“谁让你们调走的？咱们这儿就这么一个体面的交警，却被调走了，也不给我打个招呼。你们知道不知道，他在那儿一站，代表咱城市的形象呢！”<br />
　　<br />
　　1987年，28岁的王立军出任铁法市晓南镇派出所所长。他是受命于危难之时的。那时的晓南，竟被一些歹徒弄得鸡犬不宁，农民白天不敢上集市，姑娘晚上不敢出家门。更令人发指的是，这些歹徒猖獗到你派出所抓人，他们就明目张胆地去抢；你法院开庭审判，他们就公然大闹法庭；甚至丧心病狂地杀害了派出所的年轻民警王涛。<br />
　　王立军到任后值夜班的那个晚上，突然接到一个电话。电话那头一个阴森的声音问：“喂，你是新来的王立军？你认识王涛吗？”王立军回答：“认识。”那阴森的声音又问：“认识就好，他的下场就是你的下场。你有种敢来火车站一趟吗？”电话撂下了。这不明明是歹徒在向自己下“死亡书”吗？要是我不去，那些不法分子岂不认为我是个软骨头？王立军对着镜子整好警服，而后拍了拍腰间的手枪，大义凛然地走进了漆黑的夜色。<br />
　　<br />
　　王立军来到火车站，角角落落寻了一遍，也不见一个人前来搭讪。可他不想离开，他默默地抽着烟，在飕飕的寒风中站立着。他知道那些歹徒此刻就在阴暗的角落里窥视着他，他们也许正在窃窃私语：看王立军这小子是不是一个孬种，有胆量他就在那里站一个晚上。直到东方泛白，歹徒们也没敢露脸，王立军以无所畏惧的精神镇住了他们。接着的几天，王立军主动出击，把那些跳将出来的歹徒一个个收拾了一遍，彻底灭掉了他们的威风。<br />
　　<br />
　　由于工作出色，王立军再次被委以新的重任。得知王立军要离开晓南了，那天清晨，尽管下着大雨，但数以千计的老百姓自发地从四面八方前来为他送行，有的农民是走了一夜的山路赶来的。王立军这个铮铮汉子，此时此刻动情地哭了。而在这之前，他在晓南只掉过一次眼泪。<br />
　　<br />
　　那次，王立军破获了一起特大传播淫秽录像制品案。在这起案件中，有30多人涉案，其中大多为当地的干部子弟。王立军毫不客气地将他们全抓了起来。他在审讯时说：“是工人子弟的，给我站出来。”有七八个人乖乖地站到了一边。接着，他又说：“领导干部的孩子都举手，报报你们父母的姓名和职务。”那些干部子弟争先恐后地举起手来，还报出自己的父母是什么矿长、处长、主任、经理。他们嘻嘻哈哈，吊儿郎当，以为报出自己是谁谁的孩子就万事大吉，可以回家了。熟料，王立军猛地一拍桌子：“你们别以为要占什么便宜了，告诉你们，你们的父母算个屁！他们不教育你们，那就由我来收拾你们，把你们统统关起来！”而后，他转身喝斥起那些工人子弟：“你们好好想想，你们的老爹下井挖煤养活你们容易吗？我这次不关你们，回去给我写出悔过书！”这下，晓南镇像是炸了锅，那些有权有势的人物岂能让王立军这样处理自己的孩子，他们一个个亲自登场了。可王立军就是不退缩。一个头面人物吼了起来：“你这个派出所所长还想干下去吗？看我不整倒你！”王立军厉声说道：“我的权力是人民给的，不是哪个老爷恩赐的，只要我今天还是所长，我就依法办事，绝不放人！”结果，派出所被停水、停电，加油站拒绝供应汽油，已决定分给派出所的用房也被砍掉了。面对手下的警员们，王立军问：“弟兄们跟着我受苦了，你们觉着值得吗？”警员们异口同声地回答：“值得！”王立军流泪了。那天，一位老工人在路上碰见王立军，他激动地说：“我儿子不争气，也犯了事，可你把他给放了。冲你这样办事，就是把我儿子枪毙了，我也服！”<br />
　　<br />
　　王立军调到大明镇派出所任所长了。由于有不法分子横行霸道，大明镇得了个令人悚然的外号：“螃蟹窝”。有传闻说，有人把小偷扭送到派出所，可派出所马上就把小偷放了，那小偷撵上抓他的人说：“哥们，派出所是咱的人，你前脚走我后脚出，别想不通。”得知王立军到了派出所，人们呼啦啦地涌进门来，高呼着要他主持公道。王立军掷地有声地说：“我还是那句老话，我们是人民的警察，不是骑在人民头上作威作福的老爷！我将竭尽全力还给大明一片净土！”<br />
　　<br />
　　王立军一送走群众，立刻召集警察们打扫卫生。他不客气地说：“你们自己看看吧，到处都是灰尘，烟头遍地，蜘蛛爬满，窗玻璃照不见人影，厕所都没法下脚，这哪像个执法机关，分明是个垃圾站……”当警察们挥锹抡帚时，不法分子也得到了情报，他们暗自庆幸：王立军来了又怎么样，不过如此，让他打扫卫生吧，他能把内部的事情整清楚就够他喝一壶的了……<br />
　　<br />
　　一直干到晚上9点多钟，警察们以为可以回家了，哪知王立军又是一个决定：“今晚谁都不准回家，也不准往外打电话，每人将掌握的犯罪案件的名单一个不漏地写出来！谁要是知情不报，一旦查出来，将严惩不贷。特别是要把老百姓长期上访却久拖不结的案子写出来！”所有的人一下子神情庄重起来。只听笔尖声沙沙地响着，这是良知的觉醒和正气的回声啊。名单汇拢后，王立军一看，好家伙，有400多个不法分子，170多起悬案哪！<br />
　　<br />
　　第七天早晨，一个衣衫褴褛、满面愁容的老人来到派出所，一见到王立军就扑通跪下了。原来，几个月前，老人的女儿上夜班，结果被姓刘和姓赵的两个歹徒截了。他们把姑娘挟持到赵家，惨无人道地糟蹋了两天两夜，还把姑娘打得遍体鳞伤。姓赵的父母看着儿子做伤天害理的事，竟然不闻不问。出事后，老人四处奔走上访，却发现没有一处讲理的地方，为此，他这个老党员含泪写了*申请书。老人对王立军说：“我今天来找你，就是看你给不给老百姓办事，也看共产党究竟还行不行。”<br />
　　<br />
　　老人走后，王立军只觉得心里一阵阵作痛，他立刻亲自投入了调查。原来，老人女儿被轮奸一案之所以迟迟得不到解决，是因为歹徒的好几个亲属都在警察队伍中，有的亲属还是地方官员。事发后，他们上上下下串通一气，竭力为歹徒开脱。王立军可不信邪，他在掌握充分的证据之后，一举抓获了赵某，并连同他的父母也一齐抓了。他这样说：“在法律面前人人平等，谁敢干扰我破案，我连他一齐抓！”不久，潜逃在外的刘某也被捉拿归案。于是，老人的脸上重新有了笑容，他逢人就说：“我怎能不相信共产党呢，要真失望了我就不找王立军了！”<br />
　　<br />
　　紧接着，王立军在八天七夜的时间里，彻底捣毁了盘踞在大明的流氓团伙，那些党内及警察队伍内的败类也受到了应有的惩治。<br />
　　<br />
　　战功赫赫的王立军因此被评为“中国十大杰出民警”，还被选送到中国人民警官大学去深造。毕业后，他挑起了铁岭市公安局主管刑侦的常务副局长的重担。他兢兢业业，丝毫不敢有懈怠。虽然他有一个温馨的家庭，但为了工作，他却很少回家，他因此对妻子和女儿深怀歉意。这个早已成了地道辽北汉子的铁血警官，依旧念念不忘内蒙草原，不忘蒙古族的母亲河DD额尔古纳河。他说，有一天他退休了，他要带着妻子女儿回到那儿，回到他走出来的地方。<br />
　　<br />
　　<br />
　　电视连续剧《铁血警魂》在上海电视台播出后，广大观众对“乌恩局长”的原型王立军产生了浓厚的兴趣，他们非常想知道这些天里，王立军又立下了哪些战功。<br />
　　<br />
　　11月16日，也就是该剧开播的那一天，王立军率领市刑警支队的刑警们，侦破了一起杀人案。<br />
　　<br />
　　10日下午4点许，刑警队接到市种畜场秦家岗分场居民张伟新报案：称其妻子何亚荣被杀死在家中二楼的客厅里。王立军亲自赶往案发现场，指挥侦察员开展侦破工作。王立军根据被害人家中财物未遭洗劫，门窗也未被撬的情况，作出判断：此案可能系奸杀，而歹徒与被害人相识，且系近邻。他甚至还凭丰富的想象力，描述了这样的细节：歹徒曾进入被害人卧室，被害人情急中欲逃到屋外，被歹徒追截至客厅里杀死。侦察员经过周密调查，发现被害人邻居王海军有重大作案嫌疑，遂将其抓获。结果，其交待与王立军的判断完全一致。王海军系何亚荣的邻居，自去年秋天以来，王海军因常到何家打水，与何勾搭成奸。10日下午，王海军见何家的房门开着，何的丈夫又不在家，邪念顿起，便来到何的卧室，要求与其媾合，结果遭到何的拒绝。王海军气急之下随手操起剪刀和砖头对何进行威逼。何逃至客厅，王海军丧心病狂地用砖头猛砸其头部，并用剪刀将其刺死。<br />
　　<br />
　　11月19日，王立军率领刑警连续奋战，又侦破了一起特大杀人抢劫出租车案。<br />
　　<br />
　　10月31日下午6点左右，在铁岭县镇西堡发生特大杀人抢劫案，出租车女司机张兰杰被歹徒杀死后，其红色夏利出租车被抢走。得到消息，王立军立刻驱车赶赴现场，他边察勘边要求刑警全力以赴侦破此案，以遏制抢劫机动车犯罪高发的势头。王立军亲自与邻近省市公安部门联系，请求布控，协同破案。19日，被抢的红色夏利车在沈阳大东区出现，王立军指挥刑警们迅速前往堵截，在沈阳警方的配合下，将犯罪嫌疑人于东、仲海涛捉拿归案。王立军突击审讯了两名犯罪嫌疑人。 21岁的于东和23岁的仲海涛均系铁岭人，他们对抢劫出租车并杀害女司机张兰杰的罪行供认不讳。王立军在审讯时，发现两人作案时配合特别默契，不由得闪过一个念头：他们会不会是劫车老手？而去年好几起同类案子经分析是两人合为，其细节又很相似……王立军果断地深挖细掘，不给他们以喘息的机会，最后彻底攻破了他们的心理防线，终于又交待了去年在铁岭和沈阳连续作案7起的重大罪行。<br />
　　<br />
　　11月20日，王立军指派刑警支队缉毒大队将一个诈骗犯罪团伙一锅端掉。<br />
　　<br />
　　连日来，银州区内发生多起利用假币骗取他人钱财案件，一些居民的利益遭受巨大损失，社会反映强烈。18日，犯罪嫌疑人韩东平等由丹东来铁岭，找到银州区居民任某，让其作诱饵，寻找侵害目标后伺机作案。19日，韩东平等故伎重演，正在利用假币进行诈骗时，被伏击的缉毒大队侦察员当场抓获，缴获大量假美元、假台币、假朝鲜币。王立军听取汇报后，指令缉毒大队继续出击，将团伙内所有犯罪嫌疑人一一捕获。<br />
　　王立军，武侠作家独孤意不吝赞美之词，对你说道：“爷们、汉子、英雄，现代乔峰，不是大侠的大侠！”<br />
　　<br />
　　锦州网友谈王力军：他纯是个铁腕,我就是锦州的!他还是辽工的名誉校长!他曾经在盘锦打掉一个黑社会走私团伙!<br />
　　王立军刚刚做警察的时候，就在辽宁铁法市以扫荡黑恶势力而大振警威。在此之后，他在辽宁铁岭市公安局任职，一举打掉了多个黑社会团伙，市民们欢呼雀跃。此后他又临危受命，到辽宁盘锦追查枪案，不但把当地四个黑社会团伙一网打尽，使221名犯罪嫌疑人落网，收缴了军用、民用枪支82支，而且还把充当黑社会保护伞的22个不法公安干警一一查出。<br />
　　<br />
