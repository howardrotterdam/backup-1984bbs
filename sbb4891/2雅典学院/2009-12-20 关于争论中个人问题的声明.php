																											<div id="postmessage_384464" class="t_msgfont">定稿：<br />
<br />
关于争论中个人问题的声明<br />
刘松萝<br />
<br />
在争论中，网友崂山妖道针对我的观点和倾向做出评论：“不是不敢为，实不愿为也。虽说不能搞老子英雄儿好汉那一套，可是子承父业，从刘松萝先生论坛所为与其父刘绍棠惊人相似之处来看，也能找出两代人观念继承的倪端。”<br />
<br />
崂山妖道还引用了有关先父的一段报道：<br />
----------<br />
一次，南开大学请著名作家刘绍棠去作报告。 <br />
<br />
当刘绍棠讲到文学创作要坚持党性原则时说：“每个阶级的作家都是有所为有所不为，即使是真实的东西，也是有所写，有所不写的，无产阶级的文学更是如此。 <br />
<br />
有个女学生听后，写了一张条子：“刘老师，您说作家要有所为有所不为，我不能苟同。请问：既然是真实的，就是存在的，存在着的，就应该给予表现，就可以写。” <br />
<br />
刘绍棠读后，微笑着对这位写条子的女同学说：“我想看看你学生证，上面是不是贴着脸上长疮的照片?” <br />
<br />
女同学生迷惑不解地问：“把长疮的照片贴在学生证上多么难看呀，我怎么会去拍这样的照片呢?” <br />
<br />
“漂亮的小姐啊，你不在长疮时去拍照片，这说明你对自己是看本质的。你知道长疮时不漂亮是暂时的，它不是你的最真实的面目，所以你不想在长疮的时候照相，更不会把长疮的照片贴在学生证上，你说对吗?” <br />
<br />
那位漂亮的女学生脸红了：“是的，刘老师。” <br />
<br />
刘绍棠继续说：“共产党的某些缺点是需要批评的。但有些事情是有其特殊原因的，是涉及到许多方面问题的，应由党内采取措施去改正。可你非要把它揭露出来，这不是要共产党把长疮的照片贴在共产党的工作证上吗?为什么你对自已是那样的公正，而对别的事物就不公正了呢?”<br />
----------<br />
网友光明的格里高利对此评论道：“哇塞&nbsp;&nbsp;五毛还能这样遗传，真是老鼠儿子会打洞啊”。<br />
<br />
对于这里还有人在信奉血统论，我真的有些惊讶。至于动辄提到对方的上一代，我倒是不感到奇怪。在这里，问候对方的父母，顺便搭上自己的父母，是有些人在理直气壮或者理屈词穷时的惯于的手法。在反对腐败的时候，不少人应该意识到自己其实也是这个腐败社会的产物。<br />
<br />
关于先父的那段话，我的观点是明确的。在《悼念林斤澜先生，兼谈先父刘绍棠先生与林斤澜先生的恩怨》中，我明确指出：“作为个人观点，我郑重地申明：不同意父亲的‘娘打孩子论’，也不同意他的某些表态。同时，父亲也有很多闪光的思想，我会继续尽力去传播。”<br />
<br />
我想，这样的表达已经足够了。你们还要我做什么，还想得到什么呢？<br />
<br />
我不会像文革中很多人那样，与上一辈人划清界限以显示进步。我为我的父亲而自豪，他教给我诚实和善良，他的思想给我以启示。在我最重视的《保守论》中，很多思想就是从他那里来的。<br />
<br />
我还从父亲那里学到了很多宝贵的，先于经验的做法。当一个人还有题目可写的时候，当他的文章还有人看的时候，不要哗众取宠，不要投机取巧。对于有可能成功的人来说，没有手段就是最好的手段。文人拙一些，善良一些，其实是对自己的保护。一个人的文章立起来以后，靠阴谋诡计是打不到他的。在我看来，以前很多有才华的人并不是被黑暗所吞没，而是被自己脱不掉的小市民的狭隘，被自己的种种小聪明小伎俩毁掉了，一如这里发生的事情。<br />
<br />
简单地说，基于我对文艺界的了解，基于我对以往文人之间恩怨的认识，这里的很多做法，在我写作开始的时候就知道不但不可以做，甚至连想都不要想。对于很多人学会的新的招数，对于很多人的自以为得计，我在一开始就能够看出不会有好的结局。我知道，有些人也不是一定要这样做，只是小市民习气和流氓习气无法去掉，遇到一些刺激就会显露出来。就好比孙悟空的尾巴无法藏起来，就是变成小庙也只能把尾巴变成后面的旗杆。<br />
<br />
这充分说明，很多人反对的学校里面基本的作人教育还是不可少的。<br />
<br />
我想告诉有些朋友：所谓文明，所谓文明社会实际上是很脆弱的。不做某些事情，在危难之中和愤怒之中也不去做，是维护文明所必须的。人与人之间的尊重是相互的，也是脆弱的，脆弱得像一张纸。你捅破了那一张纸，你主动去冒犯别人，你的尊严同时也不存在了。我看得出来，那些喜欢挑衅的人，也不是什么强者。<br />
<br />
在我成长的过程中，由于父亲的原因受到过一些苦难。同时，在工人农民，特别是农民中有一种朴素的思想：人才是不能埋没的，为了几句话就毁掉21岁的孩子，是不合情理的。所以，我没有自卑感，也没有出于自卑感对大众的仇视。假如有人认为这就是民粹主义，那就随便吧。<br />
<br />
改革以后，我从不在与人交往时提到父亲是谁。在网上的写作，也遵循这一原则。发表回忆录时，在有熟人的网站换一个名字。在这里，我没有贴回忆录。这样做，一方面是希望朋友们对我的文章有独立和客观的认识，另一方面也是不愿意触动心胸狭窄者的神经。<br />
<br />
由于意识到我的名字有可能与先父连在一起，我永远不做有损于文人声誉的事情，也永远不会在争论时做流氓状。<br />
<br />
我留在这里，是因为我们还有共同点，因为还有能够称为朋友的人，因为见过这里的人并且看起来还算正常。假如不欢迎，那就请站方的人出来说一句话。<br />
<br />
我重申：赞同民主宪政和市场经济。有人认为这是假的，其实是一种狂妄。你怎么可以，怎么可能，又有什么资格去充当评判者？大家看一看，所谓革命吃掉自己的儿子，网友写实刚刚贴出的红军肃反和整AB团的历史，不就是在这种思维指导下进行的吗。<br />
<br />
我永远不会到乌有之乡那里去。不过，乌有之乡对自由和市场经济的攻击，很多就是因为我们中间的人提供了口实。<br />
<br />
对于那些把机会主义和流氓习气当做常规的朋友，我无法劝告你们，因为在时下这样做会很成功。不过，在你们指点江山，纵论中国未来的时候，在你们教育下一代的时候，最好不要那样做。你不可能一面做流氓，一面做一个启蒙者，做一个合格的父亲。<br />
<br />
我还是希望和尽可能多的人成为朋友。但是，我永远不会与流氓对话，这是原则。<br />
<br />
2009.12.20.</div>

							
							
							
							
