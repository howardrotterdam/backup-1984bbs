既往不咎、来者可追<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　恐怕很多人会说，温家宝等人维护一党专政而抵制宪政民主，只不过是揣着明白装糊涂。他们由“屁股决定脑袋”，由他们在党国体制中的地位决定了他们要拼死维护现存体制和既得利益。这种观点初看起来很深刻，但我素来反对历史决定论和诸如此类的“阶级分析”。历史上从专制体制中冲杀出来的开明人士不计其数，从既得利益集团中觉悟起来的先驱人物不计其数，从专制政权通过妥协而和平地转变为民主政权的例子也不计其数。在苏联东欧埋葬党国体制的过程中，党国要人在关键时刻都纷纷顺应潮流和时势站到了历史的正确一边，负隅顽抗、拼死维护党国体制的，只有齐奥塞斯库等个别顽固不化的死硬分子。社会个体具有超越能力，生平志向与人生选择并非先定。杰出人物完全能够成为“超越者”（王力雄曾提倡“超越者联盟”，见王力雄：《以超越者联盟突破精英联盟》），超越眼前利益和本阶级偏见而追求真理和更大的善。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　网上流行的一篇《社会主义宣言》这样描述当今中国的现实：“贪腐无度、傲慢的官僚，自私自利、鼠目寸光的资本家，失去了道德和信仰的知识分子，为了基本生存而苦苦挣扎的大众，这就是当前中国社会的阶级构成；权力所支配的资本像四处漫溢的洪流，以全面性水土流失的代价制造了一片沙滩上的绿洲，官僚集团与学术精英结成联盟控制了社会的命脉，腐败制度化而成为既得利益集团的共识，被剥夺、被忽略者眼中的冷漠与无奈正在变成仇恨与嘲讽，这便是当前中国社会的政治图景；官员不再忠诚，工人不再勤恳，农民不再朴实，老师放弃了尊严，学生没有了激情，所有美好、高尚的价值都遭到轻蔑，投机的、市侩主义的人生哲学成为唯一的信仰，这便是当前中国社会的精神状况”。这样一种描述在某些方面或有过于愤世嫉俗之嫌，但离中国的现实不会太远，甚至还没有将政治迫害和政治高压列入其中。“权贵资本主义”和“社会溃败”可以说是对这种描述的理论概括，民怨沸腾和此起彼伏的“群体事件”则可视为这种描述和理论的注脚。<br />
&nbsp; &nbsp; 　　<br />
&nbsp; &nbsp; 　　针对这种触目惊心的残酷现实和末世景象，党国庸人们顽固地全力“维稳”，但也激发出两种几乎截然相反的因应之道。一种是民粹主义的因应之道或情绪宣泄，仇富裹挟着仇官，甚至于仇洋、仇智。民粹主义者们将资本主义视为社会不公的根源，既以劫富济穷相号召，又连做梦都渴求发家致富；既无限地美化“人民”，又狂热地呼唤领袖、神化领袖、装扮领袖。（参阅吴稼祥：《民粹一咳嗽，大众就发烧》）一些富于心机的政客看到了民粹主义的“威力”，已经开始利用这种“威力”在政治上呼风唤雨。中国毛泽东主义共产党、中国工人共产党、《乌有之乡》网站、以及一些“新左派”人士，将等级森严、残忍无道、蒙昧无知、民众赤贫如洗的毛泽东时代幻化成平等、高尚、幸福的天堂，借以宣泄对现实的愤懑。更富于戏剧性的是，出身太子党、在文革红潮中深受迫害而在涉足官场以来一直借助黑道之嫌的薄熙来，居然铤而走险，在重庆大举“唱红打黑”以争取民心。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　追求社会公正的底层抗争，具有神圣的正义性。但是，民粹主义的致命伤在于，经济上反市场经济（资本主义）、政治上反议会民主（宪政民主），与人类现代文明基本抵触，根本无法为底层社会和民众利益找到出路。在市场经济和宪政民主框架下的民粹主义，可以发挥补充性的有益作用，以民粹主义来抵消或摧毁市场经济和宪政民主，则将万劫不复。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　与民粹主义形成对照的是自由主义的因应之道。当代中国思想和学术界的自由主义学派，阵容庞大、群星灿烂，从政治主张到学理阐发，都已形成完整的体系。这个学派秉承个人平等权利至上的自由主义真传，认为当代中国社会病弊的基本根源是一党专政的落后政治体制，认为这种落后的政治体制是吏治腐败、两极分化和社会治理、环境治理、法治建设等等方面都无法走上正轨的总根，因而坚决要求废除一党专政，建立市场经济和宪政民主。归纳中国自由主义的思想主张而直接面对朝野上下社会大众的最佳通俗版本，莫过于《零八宪章》。这份文献所阐述的自由、人权、平等、共和、民主、宪政等六个基本理念和修改宪法、分权制衡、立法民主、公器公用、人权保障、公职选举、城乡平等、结社自由、集会自由、言论自由、宗教自由、公民教育、财产保护、财税改革、社会保障、环境保护、联邦共和、转型正义等十九项主张，虽然在精确性和全面性等方面仍有可议之处，但基本上汇集了当代中国自由主义和社会民主主义的成果。中共党内自由主义的一些领袖人物，出于策略考虑不在这份文献上签名。但是，诸如朱厚泽对“权为民所授”的呼吁、李锐对“宪政大开张”的期许、江平对“私权”和“法治”的呐喊、吴敬琏对“法治的市场经济”的呼唤，他们的思想主张也都体现于《零八宪章》之中。<br />
&nbsp; &nbsp; 　　因为不知道自己的历史使命，温家宝在民粹主义和自由主义两者之间彷徨徘徊。近年来温家宝对自由主义价值理念的赞赏，常常语惊四座。在2007年2月26发表的题为《关于社会主义初级阶段的历史任务和我国对外政策的几个问题》一文中，温家宝写道：“科学、民主、法制、自由、人权，并非资本主义所独有，而是人类在漫长的历史进程中共同追求的价值观和共同创造的文明成果”。他在2007年3月16日的记者招待会上再次重申：“民主、法制、自由、人权、平等、博爱，这不是资本主义所特有的，这是整个世界在漫长的历史过程中共同形成的文明成果，也是人类共同追求的价值观”。温家宝对自由主义普世价值的认同，掷地有声，向世人透露自由主义价值理念已经进入他的灵魂深处。但是，温家宝在落实这些自由主义价值理念的经济改革和政治改革方面，又几乎毫无建树。他似乎不明白，落实公平的财产权以及其它方面的平等权利，是全面改善民生的必要条件。因而，他在具体施政过程中往往沿着民粹主义的思路，将政府恩赐的福利抵消公民权利的落实。他可以在广大农民抗捐抗税的压力下减免农业税，却拒绝先进农民们对土地所有权的要求、拒绝废除取消极不平等的户口制度；由于屈从于保护“公有制”（实际上是体现权贵垄断经济“官府所有制”）的民粹主义呼声，温家宝在总理任上不但没有努力推进市场化改革，反而为祸国殃民的“国进民退”推波助澜。他似乎也不明白，言论自由、结社自由等等是公民不可让渡、不可剥夺的神圣权利，而且为有效低监督和制约政府所必不可少，没有着手落实这些公民权利，而是热衷于“体察民情”的信访和“平息民愤”的表演性惩治腐败。<br />
&nbsp; &nbsp; 　　当然，在现在的党国寡头制权力格局中，温家宝会遇到力不从心、无能为力的困惑。那些因为黑幕丑闻缠身而对民主化深怀恐惧的“常委”，难免会与温家宝同床异梦。比如，分管意识形态大权的李长春不仅封杀普通民众的言论，甚至也封杀温家宝关于自由民主的言论、组织人马围攻温家宝所公开赞赏的“普世价值”；分管政法大权的周永康，不断部署对维权人士、维权律师、民间NGO的严厉打击。<br />
&nbsp; &nbsp; 　　正因为有这样的内部掣肘和官僚集团的抵制，改革大业才那么崇高、那么需要勇气和魄力。是否温家宝已注定无所作为？赵紫阳下野之后曾对温家宝这一昔日下属下过这样的断语：“胡和温都是好人，但都缺乏理念，缺乏历史抱负和眼光”（宗凤鸣记述：《赵紫阳软禁中的谈话》，第344页）。这样的断语自是中肯，但一个人要到盖棺才能论定。赵紫阳本人也是到政治生命的最后岁月才确立起对议会民主的理念。温家宝毕竟是在位总理，在党国正式的权力机构中排列于第三位（其实总理的实际权力远远大于人大委员长），任期也还有两三年。如果他能与党国开明势力联手奋力一搏，并与民间健康力量良性互动，或许还能扭转乾坤、开辟改革开放事业的新局面。他这样作当然有些风险，但天底下哪里会有零风险的大事业？对温家宝个人而言，他的风险又能有多大呢？改革事业不成功，确实有可能会让他下台。但他不是再有两三年就终究要退职下台吗？他不是常常引述林则徐的诗句以明其志，要“苟利国家生死以，岂因祸福避趋之”吗？他不是常常引述王安石的名言以壮其胆，要“天变不足畏、祖宗不足法、人言不足恤”吗？那么，温家宝何以牢骚满腹而无所作为呢？既然成功不必在我，缘何不大胆尝试？既然没有尝试，又能焉知成败？<br />
&nbsp; &nbsp; 　　一党专政的党国体制，党国权贵对政治、经济、社会、文化资源的垄断格局，不会长命百岁。而且，我具有悠久历史和智慧的中华民族，决不是只会在专制政治和天下大乱（无政府状态）两条死路之间选择。古今中外历史的演进和这三十年来中国市场经济自身的发展，都再也明白不过地指明了中国未来发展的道路是市场经济＋民主政治＋开放文化。违反市场经济的“国进民退”是一条邪路，违反民主政治的一党专政更是走邪路。中国实现宪政转型的社会历史条件早就具备，包括初具绉型的市场经济、普遍觉醒的公民权利意识、高度发达的交通和媒体等等。中国宪政转型的历史大潮，正在遭遇最后的“政塞湖”和“言塞湖”，正是朝野上下有识之士为疏通这两个死湖而大显身手的时候。中国民间也已经更加成熟，绝不会再象二十世纪八十年代拒绝配合赵紫阳等党内健康开明力量那样幼稚可笑。只要温家宝或任何党内开明人士作出变法改制的努力，都会得到民间的全力支持。有识之士当前所担忧的是，不到局势失控的时候，当局不会下决心改弦更张；到了局势失控的时候，则已经没有机会改弦更张，可能走向玉石俱焚的结局。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　当历史任务已经很明确地提上议事日程的时候，解决历史任务的条件也已清晰地出现于地平线。实际上，温家宝有几条现成稳妥的路径推进中国走向市场经济和宪政民主的事业。一条是在他所分管的经济领域，坚定不移地落实农民的土地所有权和促进民营经济的发展。既然认定了市场经济这条路，至少也应该落实市场经济所必须的私有产权（包括土地资产和金融资产）并使市场在资源配置中其基础性作用。对土地及其它支配国计民生的行业实行官府垄断，既违反市场经济的基本原则，也造成不公和腐败。国人在党国前三十年的不断折腾中穷怕了，在这后三十年，一当党国稍微松绑、一定程度上允许市场经济的发展，国人的致富欲望、致富动力和致富能力便象火山一样迸发，并且借助全球化和技术、产业大转移的东风，创造出惊人的财富。遗憾的是，党国一党专政的权力格局没有改变，官府支配关键资源的利益格局没有改变，市场经济被惊人地扭曲，形成官僚统治集团的结构性寻租和腐败、资源分配的极端不公、经济发展的严重不平衡、对弱势群体的残酷压迫和掠夺、对生态环境的自杀性破坏。中华民族没有理由不对这种腐朽落后而又不公不义的党国体制作一了断。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　另一条路是支持民间方兴未艾的民间维权运动，并与维权律师配合，将政治问题法律化的改革路径，通过修改或废除有违宪法和法治精神的行政条例及其它恶法，一箭双雕地民主和法治。2003年由清理和废除《城市流浪乞讨人员收容遣送办法》，开了一个好头。应该立即清理或废除的“恶法”至少包括：户籍制度、劳动教养制度、城管制度、《刑法》中歧视律师和犯罪嫌疑人的条文、《治安管理处罚条例》、《土地管理法》、《城市房屋拆迁管理条例》、《出版管理条例》、《互联网上网服务营业场所管理条例》等。所谓“恶法”，就是违反法治精神的法律和规章制度。只有依照法治精神对这些恶法进行系统的清理，才能使法律真正成为限制政府权力、制约黑恶势力、保护公民神圣权利和利益的工具，才能确立起公民对法律和法治的信心，才能将中国真正建立成为公正和谐、文明有序的现代社</div>

							
							
							
							
