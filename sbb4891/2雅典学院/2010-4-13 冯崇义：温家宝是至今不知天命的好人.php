													<h2>冯崇义：温家宝是至今不知天命的好人</h2>
						
						
																											<div id="postmessage_560656" class="t_msgfont">(博讯北京时间2010年4月12日 首发 - 支持此文作者/记者)<br />
<br />
&nbsp; &nbsp; 冯崇义更多文章请看冯崇义专栏<br />
&nbsp; &nbsp; 日前与一南开校友小叙，谈到近来互联网上对校友温家宝日益增多的尖锐批评和讽刺。这些批评和讽刺，主要集中在能力和德性这两个方面，不仅认为温某乃无能之辈，只会办小事、说空话，而且认为温某是个伪君子，惯于用虚情假意的眼泪作秀、骗取同情，他那些反腐倡廉、颂扬自由民主的言谈也都不过是政治秀。<br />
&nbsp; &nbsp;&nbsp;&nbsp;(博讯 boxun.com)<br />
<br />
<br />
&nbsp; &nbsp; 　　这样解读温家宝，非常不利于他与民间的良性互动，似乎也有欠公允。在我看来，温某所欠缺的不是能力，而是魄力；他有关自由、民主和民生的大胆言说，本身就是重大行动(speech-act)，不能贬为空言；温某两眼常含泪水，绝非都是作秀，而多是真情流露连作秀用的也是真情。温家宝的不幸在于，由于知识结构和魄力上的欠缺，他不知道他的历史使命（天命），更谈不上捉住机会为这种使命拼死一搏。<br />
&nbsp; &nbsp; 　　<br />
<br />
“平民总理”牢骚满腹<br />
&nbsp; &nbsp; 　　<br />
&nbsp; &nbsp; 　　温家宝是来自民间的“布衣宰相”（“平民总理”）。他的这种不同于世袭权贵的身份与经历，至少有两点可资论列。其一，温家宝“有才”。专制政治是一种逆淘汰机制，自动地淘汰出类拔萃、棱角丰满的才俊，智勇双全的旷世之才没有机会在党国中成长起来。但是，论个人才干，温家宝确实是当今党国官僚中的佼佼者。他没有得益于任何特殊关系的荫庇，在对党国的政治忠诚方面也没有过人之处，他的升迁靠的是能力与机遇。特别是他因为在中央办公厅担任要职期间跟胡耀邦、赵紫阳两位失势的总书记多少有些瓜葛，若不是有过硬的能力和处世本事，在反胡赵的时期升到总理高位，根本不可想象。南开的校训是“允公允能，日新月异”，温家宝应该说是颇得南开真传。他在追求公德和卓越能力方面，有明显的成就。别的不说，温家宝在言谈举止中所展示的人文知识修养，足可使很多文科教授汗颜。他2006年11月在全国作家代表大会上的那篇讲话，对古今中外几十种经典著作和古今中外几十名作家，旁征博引、随手拈来，其娴熟程度连当代文学界高人沙叶新也叹为观止，足显其读书之博、记忆之强、积学之厚、绝顶聪明。特别令高人沙叶新惊叹的是，温家宝在这篇讲话讲话中引述了一些中共党内高层视为异端的人物与言论，诸如赵丹的临终遗言、冰心的“风骨”、巴金的《随想录》、吴祖光和新凤霞的蒙难。（沙叶新：《我在香港学习温总理讲话》）南开的“日新月异”之训，要求的是及时更新知识、及时革故鼎新。温家宝上任以来，也不乏对中国落后于时代并充满危机的忧患意识，只是迄今没有采取大刀阔斧地进行革故鼎新的举措。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　其二，温家宝与世袭权贵有隔阂。按照高新的观察与研究，江泽民对温家宝有知遇之恩。（高新：《温家宝传》）温家宝当上总理之后，与江泽民及江系人马多有龃龉，在一定程度上可以解释为清廉的平民总理与贪腐的世袭权贵之争。坊间关于温家宝家属贪腐的传闻也并不少，甚至还有人还将温家宝称为“贪官污吏的总头目”。但是，这类传闻或者是温家宝的上层政敌故意造谣惑众、混淆视听，或者是道听途说，并没有确实依据。温家宝属于古来中国注重清誉、严于律已的那一类官员。其夫人张培莉曾卷入珠宝生意，为了避嫌，温家宝足遍五大洲而从未携带夫人同行，甚至有传闻说他们已经分居。其子温云松既不走从政之路乘机升官发财，也不当国企老板仗势欺行霸市，而是自我创业、自谋生路，并没有违纪牟利之举。坊间关于温云松接受某保险公司巨额资金的指控，不可当真。温家宝在处理周正毅案、陈良宇案及其它贪腐案件中的举措，得罪了不少人。温家宝在官民冲突的一些场合，站在弱势群体一边说话，也难免会令一些官员咬牙切齿。其结果，便是温家宝虽贵为总理，但在党国官场势孤力薄。这样，他就只能可怜巴巴地替农民工“讨”薪，而不能义正词严地命令拖欠农民工血汗工资的企业付工钱；他就只能常常在一些救灾前线说一些空话和套话，而不能命令当地官府全力以赴；他就只能牢骚满腹地央求“人民”军队的将官们“看着办”，而不能坚决要求“人民养”的军队尽心尽力帮助人民抗震救灾。<br />
&nbsp; &nbsp; <br />
<br />
不知天命、错失良机<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　温家宝好像确实很无奈。今年2月27日，温家宝上网接受网民在线访谈时说，“我的心情确实不那么紧张，倒有一种十分珍惜的感情，因为我知道这样的机会不多了”，惆怅落寞之情溢于言表。但是，温家宝当上总理已经七年有余，整个说来只是“好施小惠、不行大义”。他只是将精力全部集中于具体事务，头痛医头、脚痛医脚，而没有总揽全局、从体制上入手来解决党国体制所衍生的千头万绪的问题，疲于奔命而不得要领。对比那些浑浑噩噩、贪赃枉法的官僚，对比那些狂妄无知、以“大国崛起”来自欺欺人的官僚，温家宝保持着关心民间疾苦的良知、清廉正派的品格、清醒的忧患意识。他深知中国吏治腐败的严重程度和普遍程度、深知中国社会缺乏公平和公正的严重程度和普遍程度、深知中国生态环境破坏的严重程度和普遍程度、深知中国弱势群体和下层民众贫困和痛苦的严重程度和普遍程度。按照温家宝的智商与情商也可以断定，他深知他所面临的这些问题和忧患，包括高投入、高消耗、高污染、低人权、低工资、低福利的掠夺性经济增长方式，有着深刻的制度根源；他深知吏治腐败、两极分化、破坏环境、耗费资源等种种病弊在现有体制之下处于“不可治理”状态，也就是因为缺乏“治理”这些问题的基础制度和基础秩序，已经失控。（参阅孙立平：《中国社会正在加速走向溃败》）；他深知他的政府所采取的种种应急措施只是杯水车薪而无补于大局。但是，温家宝仍然没有在改进制度方面有任何作为，尽管在2003年就曾有“胡温新政”的呼声。2003年由孙志刚案引发的良性官民互动，导致国家行政、司法和立法三个部门联手废除了《城市流浪乞讨人员收容遣送办法》这一恶法。如果胡锦涛、温家宝能够以此为契机，乘风破浪，从保障人权、维护基本公民权利入手，系统清理那些有害于基本公民权利的行政条例和恶法，中国的法治和宪政民主秩序现在当已理出头绪。温家宝2003年以来的所作所为，似乎表明他与那些浑浑噩噩、贪赃枉法、狂妄无知的党国官僚们仍然具有根深蒂固的共性，带着共同心愿去维护现政权和现存体制的稳定与延续。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　由是观之，温家宝这位南开校友的致命弱点是不知天命，不知他的使命何在，因而不断地错过机会、虚掷光阴，兢兢业业而成就甚微。温家宝及当代中国的仁人志士，其天命（历史使命）绝不是长吁短叹、徒喊奈何，更不是维护被历史所淘汰的党国体制、延长一党专政的寿命。当代中国的仁人志士的天命（历史使命）是实现中国的宪政转型，也就是实现新历史条件下的“变法”和“改制”，结束一党专政，完成一百多年来中国数代仁人志士孜孜以求的宪政民主大业，使中国告别野蛮落后而与世界文明发达国家并驾齐驱，使中国化解民主国家的包围和防范而得到更好的发展机会，使中国顺利地实现统一并更好地维持统一。（参阅冯崇义、杨恒均：《中共的命运和胡温的使命》）光指责温家宝等人懦弱是不够的，他们的无所作为，并不都是因为懦弱。吾国古来就有“文死谏、武死战”的传统，并非都是懦弱之辈，连出于愚忠而撞死于宫廷朝柱上的士大夫也不乏其人，舍身取义的壮士有如群星璀璨。能否采取果敢行动，问题在于能否把握天命，能否认识值得他们为之献身的目标。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　温家宝等人知道的信息很多，就是不知道他们的历史使命。人们已经发现，长期接受扭曲、错乱的信息会导致“脑残”，就像中了病毒的电脑一样发生错乱，无法正常地处理信息，有时甚至必须进行“格式化”才能消除病毒恢复正常。当代党国要人们知识结构方面的缺陷，有目共睹。他们所受的党化教育给他们带来的是极为畸形的知识结构，使得作为人类现代文明之基石的自由主义理论没有成为他们知识结构的组成部分，对宪政民主的价值形成可怕的认知障碍。党国六十年来依靠“专政”的暴力对知识和信息进行阉割和过滤，对中国人的精神和思想进行阉割使之成为精神和思想上的“阉人”，对中华民族心智的破坏，罪孽深重。党国曾全面妖魔化“旧社会”，歪曲事实而编造出关于刘文才、周扒皮、南霸天之类的残忍故事，将中国历史上的富人都说成魔鬼，而使“全国人民”相信靠暴力起家并靠暴力维持特权的当朝人物才是天使；党国曾全面妖魔化西方发达国家，而使“全国人民”相信那里的资本主义和“假民主”使人民生活在“水深火热”之中，使“全国人民”在受到党国随意驱使奴役时幸福得热泪盈眶，就象今天北朝鲜的人民仍然表现的那样；党国的“爱国主义教育”所长期灌输的“帝国主义亡我之心不死”这一仇恨话语，使很多愤青愤老迄今无法正常地看待正常的对外贸易和交往；党国不断变换主题和打击对象的阶级斗争话语和“维稳”话语，更使不明真相的中国人总是跟着党国的指挥棒去仇恨和打击那些被贴上“国家敌人”标签的无辜同胞，残忍无道而又理直气壮地剥夺他们的同等国民待遇。长期的精神阉割，受害者绝不仅仅是普通“人民”，党国要人往往受害更深。对中华民族来说，告别精神阉割所造成的荒唐思维惯性和“党国逻辑”，恢复正常心智，刻不容缓。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　当我使用“变法”、“改制”这一类概念的时候，很容易使人联想起近代中国康有为的《孔子改制考》与梁启超的《变法通议》。的确，“变法”、“改制”这一类概念在一百多年前的中国就曾经是时代的强音，清末士子康有为、梁启超等人当时就已开始为中国的宪政而奋斗，而后更有清王朝的“立宪”和孙中山等人彻底颠覆“家天下”而“创立民国”、“实现共和”。如果国人不是阴错阳差地受苏俄的列宁等那些不诚实的布尔什维克的毒害，无论如何也不会在告别了专制的“家天下”之后再闹出更为专制的“党天下”来，无论如何也早就生活在宪政民主的现代文明制度之中了。党化教育所造成的一些愚昧相当奇怪。国人反对专制而赞同民主，但一旦将党国专制说成一种“新的民主”，立马热烈拥抱；直接攻击自由、平等、博爱这些价值理念，很多国人都会反感，但以攻击“全盘西化”和“普世价值”的方式来攻击自由、平等、博爱这些价值理念，很多国人就不仅不反感，而且觉得理直气壮。“党天下”明明是从苏俄武装走私进来的毒物，却至今仍被党国领导当作“中国特色”连坚持；宪政民主明明是全世界所有文明民族都走的正道，却至今仍被党国领导当作“邪路”来封锁；明明是公然违反社会主义平等原则的现存等级专制秩序，却至今仍被党国领导当作“中国特色的社会主义”来自欺欺人；明明是公然以一党专制的权力垄断和资源垄断来扭曲扰乱市场经济，却至今仍被党国领导当作“社会主义市场经济”来大吹大擂。“社会主义”被扭曲成一党专政的同义词和维护落后的辩护词，中国和中国人之不幸，莫此为甚！人们真的难以理解，以我南开校友温家宝及其他党国要人之智商，何至于糊涂如斯！<br />
