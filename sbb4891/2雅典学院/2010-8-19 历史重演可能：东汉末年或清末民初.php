													<h2>历史重演可能：东汉末年或清末民初</h2>
						
						
																											<div id="postmessage_788510" class="t_msgfont">历史总是惊人地相像，而中国的历史尤其相像得惊人。历次朝代间的转换，大同小异。汉末党锢，清末变法。暴秦骤起，二世而亡。毛氏暴政，顷刻间灰飞烟灭。邓氏改革，八九年真相毕露。物换星移，转眼间又是一场变局在即。且不说在时代的横座标上（亦即所谓国际大环境）存在着多少变数，仅取历史的纵座标（所谓特殊国情）察之，便可以发现，当今时局最为接近的，乃东汉末年，或清末民初。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;今日中国强权人物的消失，与东汉末年的皇权式微相似，权力已然出现真空。皇权式微激发天下枭雄称帝野心，强权人物的消失引诱实力者扮演乱世英雄。重庆打黑，不过小小序幕而已。不要说曹操尚未现身，即便董卓都还没有露面。当今集权钱于一身的在朝者，金钱美女已经不够刺激，青史留名才是真正的牛皮哄哄。“天下者我们的天下”，会在想要有番作为者的心里再度鸣响。小站练兵，或者江湖暴动，可能一一重演。<br />
<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;当年的宫廷权争，被诉诸勤王藉口。今日的权谋角逐，当以维稳作前提，诸玩家谁也不敢造次。皇权有姓，江山有色。袁项城逼退清室，以走向共和为标高。曹孟德挟天子以令诸侯，坚拒陈蕃式的以儒治国。就算真的一效周公吐哺，天下也未必归心。权力和话语，在八九年之后，空前分离。当朝者全体失语，未来的话语，远在天边。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;汉末也罢，清季也罢，朝廷无力奢望千秋万代永世长存。当今的肉食者更是心知肚明。钱财随家眷悄然外移，官成为官场现象也成为新的专用名词。当朝没有未来，已然成为不言而喻的朝野共识。只是担当未来者的气候，尚未蔚为大观。遥想汉末党锢精英，血气方刚，底蕴充沛。戊戌维新君子，有仓惶者，亦有就义者。八九年倒入血泊，却是满地无辜。比起陈蕃的迂执，袁项城继承了曹操的灵动。相对于曾国藩的倚天照海，孙文不过一具委琐小人。可叹中国的历史总是成为屑小的舞台。君子蒙尘，小人成圣，最无赖的流氓最终得了天下。因此，不要以为将来一定辉煌，说不定还会选择最为下三滥的结局。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;八九年以降，历史的推手从高层的政治首领，曾经风光一时的知识精英，渐次转换为此起彼伏的维权民众。知识界的先锋人物，也从当年大言不惭的牛皮家，还原为脚踏实地的维权律师。比起汉末的黄巾起义，有清的太平天国，维权标画出了崭新的历史景观。维权宣告，水泊梁山的方式，应该终结。维权要求的公平和正义，不再是“苍天已死，黄天当立”，而是不可剥夺的天赋人权，而是私人拥有的财产和权益，而是不可侵犯的人之为人的尊严。维权不再诉诸江湖造反，而是诉诸理性的方式，和平的方式，谈判的方式，讨价还价的方式。矗立在维权背后的是商业文明的构架，而不是农民起义的传统。放之四海而皆准的普世价值，不再通过知识精英的倡导，而是经由前赴后继的维权行动，成为家喻户晓的常识。倘若说八九年是某种历史的终结，那么此起彼伏的民间维权，将成为走向未来的历史开端。维权的中国民众不再迷信皇帝，志在构建公民社会，从而破天荒第一次真正践行了人民创造历史的观念；而此前诸如此类的说法，不过是张扬权力话语和服务于意识形态的说教。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;然而，历史归根结底取决于思想的创造力。几千年历史裹足不前，缘自这个民族的思想，丧失自由，了无活力。有鉴于以儒治国的教训，汉末群雄非儒崇法，以权谋和心计的较量，逐鹿中原。结果，思想流于清谈，沦为玄学。清末民初，民智重开。留洋学童，一代栋梁。西学东渐，百家争鸣。无奈批孔流于口号，法家权术借主义之尸赫然还魂。自由思想，被江湖暴力一步步窒息封杀。草莽篡改革命，主义荼毒人心。皇袍可以不穿，帝王不能空缺。千古一帝，不叫皇上，改称主席。思想落地，谎言上天。清末民初的变局，魔术般变出一个动物庄园。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;汉末失语，惟剩暴力和权谋；清末学语，有说中体西用，有说全盘西化。今朝，指向未来的话语在天边，自由的思想在流亡。学府无论中外，一律与官府同流合污。中体西用演化成儒学加马克思，全盘西化蜕变为追随德里达和萨义德，左派传统爆响朱熹下嫁毛泽东的喜剧。即便是耶稣基督，也照样被劫持过来号令天下。学问迅速转化成名声和财富，学者不再以盗窃为羞耻。思想文化的表达，有如逐鹿中原，以成者为王为原则。这个民族从来没有面临过如此深重的人文危机：制造思想的人群竟然争相为欲望所苦。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;变局在即，可是有志于创造未来的人们，真的准备好了么？且不说汉末党锢的豪气，戊戌君子的英勇，即便是曾国藩式的心胸，容闳式的热诚，甚至李鸿章式的老到，段祺端式的清廉，都是不可或缺的人文素质。拭目以待吧。<br />
