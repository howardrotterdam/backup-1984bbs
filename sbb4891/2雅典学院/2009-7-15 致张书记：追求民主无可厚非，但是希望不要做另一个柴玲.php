																											<div id="postmessage_163603" class="t_msgfont">89事件在我这里几乎没有什么印象，只是大学时老师提到此事才查阅相关资料。知道得并不是很多，因为太多无法确定，无法确定的东西，我宁愿不信。不过有一件事，我记得非常清楚，而且是千真万确的：那就是六四领袖柴玲的发言。<br />
<br />
以下摘自wikimedia：<br />
<br />
<a href="http://zh.wikipedia.org/w/index.php?title=%E6%9F%B4%E7%8E%B2&amp;variant=zh-cn" target="_blank">http://zh.wikipedia.org/w/index. ... 2&amp;variant=zh-cn</a><br />
<br />
柴玲在5月28日接受美国记者飞利浦・康宁汉（Philip Cunningham，汉名“金培力”）采访时嘶哑着喉咙声泪俱下地说：“觉得为中国人去奋斗不值得。”<br />
<br />
以下是柴玲的原话：<br />
<br />
“下一步作为我个人，我愿意求生下去。广场上的同学，我想只能是坚持到底，等待政府狗急跳墙的时候血洗。不过我相信一场大革命很快就会到来――要是它采取下策的话。即使不采取下策，保存一些火种和力量，在下一次运动中我们一定会站出来……<br />
<br />
同学们老在问，我们下一步要干什么，我们能达到什么要求。我心里觉得很悲哀，我没办法告诉他们，其实我们期待的就是，就是流血。就是让政府最后，无赖至极的时候它用屠刀来对着它的公民。我想，也只有广场血流成河的时候，全中国的人才能真正擦亮眼睛。（哭）他们真正才能团结起来。但是这种话怎么能跟同学们说？尤其可悲的是，有一些同学，有一些什么上层人士，什么什么人物名流，他们居然为了达到个人的目的，完成自己的一些交易，拼命地在做这个工作，就是帮助政府，或者不让政府采取这种措施，而在政府最终狗急跳墙之前把我们瓦解掉，分化掉，让我们撤离广场”<br />
<br />
-------------------------------------------------<br />
<br />
这就是为了民主而战斗的柴玲，本来部分同学已经有撤离的意思，她却要求同学们继续留在天安门广场，“期待的就是流血！”，自己却“作为我个人，我愿意求生下去”。<br />
<br />
1984里的大多数人对民主都有强烈的期待，我只希望张书记等人在吸引来这些人之后，能站在最基本的人权角度保护他们，而不是像柴玲一样，在关键时刻把他们当炮灰，自己却“求生下去继续传播革命的火种”。<br />
追求民主无可厚非，我本人也向往民主。只愿你能负起该负的责任，带领追求民主的同学们在追求民主的路上，学会思考和审时度世，而不是只凭一腔热情，白白牺牲。<br />
<br />
另外也对同学们说，追求民主是一个终极理想，但是却不是一朝一夕可以实现。把主要精力放在追求更好的个人生活上面更加现实，毕竟在这个社会，你的努力不会白费。把自己的生活现状不满完全归咎到社会制度的问题是不对的，毕竟仅靠自己努力就实现人生的人，在我们身边有很多。同样的制度下，他们也没有背景，也不是党，为什么他们能成功？<br />
<br />
最后想说一句：我在这里没有任何讽剌挖苦的意思。</div>

							
							
							
							
