																											<div id="postmessage_617861" class="t_msgfont">... by titan quote from <a href="http://hi.cutlife.com/topic/view/790.html" target="_blank">http://hi.cutlife.com/topic/view/790.html</a><br />
<br />
把冯小刚的那本《我把青春献给你》重翻了一遍，其中有一章节写到了艾未未：<br />
<br />
　　<br />
&nbsp; &nbsp;&nbsp; &nbsp; 我当时开的是一辆租来的出租车，车门上还印有每公里的单价，不明真相的路人常常伸手截车，有时看到几个金发碧眼的姑娘从酒吧出来，兴致所至，我们也会载她们一程，因为是免费的，所以分手时，我们和姑娘们都会有些依依不舍。我不懂英语，刚开始时也不认路，所以老得问坐在旁边的艾未未，他有时烦了就不好好指路，该拐弯时也不说话，我就一直往前开，开到哪儿算哪儿。有一次，我赌气一直开到海边，对他说:你要还不说拐弯，我就开到海里去。他闭着眼睛躺在车座上说:把玻璃摇上，等车完全被水淹没了，再逃生。我脑袋一热，差点就一脚油门轰到海里去。在岸边我刹住车以后，他认真地对我说:我特别想体会一头扎进海里去的感觉。平常开车，他也老说:撞一次吧，求求你，快点再开快点。久而久之，弄得我心里也跟着了火似的，老觉得自己开的是装甲车。终于有一次在长岛，喝了几口酒，在停车场附近，试着以20公里的时速行进，不踩刹车撞向一个小土坡，其产生的冲力令我至今记忆犹新。由此可想而知，如果是100多公里的时速撞车会是多么恐怖的一件事情。<br />
<br />
　　那一段时间，艾未未的出现使我的心里充满了野性，对秩序的破坏欲与日俱增，要不是我天生怯懦，又对未来充满憧憬，后果真是不堪设想。后来看到库布里克的电影《发条橙子》，一下就理解了那些混蛋的所作所为。<br />
<br />
　　艾未未是郑小龙请来为剧组帮忙的朋友，也是北京人，曾是“星星画会”的主要干将，于1978年就读于北京电影学院美术系，同期的学生，日后成为中国电影“第五代”。大学读了不到两年，烦了，觉得没劲了，毅然决然放弃学业来到纽约。我认识他的时候，他已经在纽约呆了12年。他是一个前卫艺术家，住在曼哈顿第一大道和第二大道之间的第七街上，那一带集中了很多像他那样不着调的艺术家，也有光头党和爆走卒，同时那一带也是纽约贩卖来路不明商品的黑市。艾未未和那一带的黑人兄弟亲如一家，彼此见面，不是FUCK这个，就是FUCK那个。他喜欢恶作剧，善于随心所欲地把两种不相干的事物嫁接到一起，使它们产生一种新的含义。比如说:他会把一个篮球装进一只编织袋中，从楼顶上抛下，看着一只编织袋在街道上弹跳，令许多不知其中奥秘的行人纷纷驻足观望，百思不得其解。再比如:他从黑人手里买到一张文革时中国出版的塑料唱片，内容是，中央台播音员字正腔圆朗读的毛泽东著作《老三篇》。艾未未找来老式唱机，接上喇叭，开足音量，让毛泽东思想嘹亮地响彻在纽约的第七街上。<br />
<br />
　　艾未未为人仗义，朋友也是五行八作干什么的都有，九二年的圣诞节前夜，我在他的地下室留宿，遇见一个韩国人来串门，刚坐下，就被艾未未从后面用塑料袋把他的脑袋套上，一边拧紧塑料袋憋得韩国人满脸通红，一边对我说:这小子是个贼，好好搜搜他，身上一定有好东西。韩国贼拼命挣脱，从怀里掏出一个纸袋子，说了一串韩国式的英语，把纸袋包着的一瓶酒郑重地送给了艾未未。<br />
<br />
　　韩国贼诚恳地说:我今天没偷东西，这瓶酒是我自己花钱买的，送给你作为圣诞节的礼物。<br />
<br />
　　事后艾未未对我说:我来纽约12年，有两件事让我体会到人间尚有真情在。一个是每年过生日，我自己有时都忘了，但大西洋赌城从来也没有疏忽过，一准寄来生日贺卡。再有就是这个圣诞节，收到贼的礼物。他强调说:一个贼，能自己花钱买礼物送人，可见这种感情是多么的真挚。<br />
<br />
　　说到艾未未和贼的感情，让我想起一件事。一天我们在他的地下室拍戏，负责外联的李争争突然跑进来，对我们说，他车上的一个价值200美元的音响被人敲碎玻璃盗走了。未未听到后，出去转了一圈，只花10美元，就从一个黑人手里买回来了一个音响，送给了李争争。李争争看见之后惊呼:这就是我丢的那个。<br />
<br />
　　那时我们两人经常开着车在长岛上盲目地东游西逛，他常常指着一座座花园洋房对我说:这些都是垃圾，应该炸掉。看到我露出不胜向往的贪婪目光时，他也会一脸坏笑地补充说:可以给你留下一幢。那时他就反对建筑和装修有任何抒情的倾向，喜欢冷酷、喜欢简单，就是现在常说的“简约”。12年前，他曾对我说:你回到北京以后买一块地，我给你设计一个房子，保证花钱不多，又非常牛逼。我现在还隐约记得他的方案，他说:你买四截加长的集装箱货柜，彼此衔接组成一个“口”字形的建筑，从外面看不到一扇窗户，甚至也找不到门，就像一个金属方块，所有房间的采光都是从里面的天井获得。我当时听了，热血沸腾，满处打听买一截最长的集装箱得花多少钱。回国后，离开了艾未未的影响和灌输，审美观再次堕落到了庸俗的轨道上来。12年后，艾未未终于在中国找到了一位勇敢的实践者，此人就是北京房地产界另类，潘石屹先生。潘石屹被艾未未蛊惑，在长城脚下，投巨资造了十几幢巨冷酷的房子，令人看上去不寒而栗。前往参观者生怕自己不识货，异口同声说“牛逼!”。一方面，极大地满足了潘总的虑荣心;另一方面，也把他的资金牢牢地冻结在八达岭的寒风里。这些冷酷的房子，如同一件打湿了的棉袄，穿在潘石屹的身上，脱下来冷，穿着更冷。<br />
<br />
　　现在冷酷和简约已经在北京蔚然成风，每次看见那种裸露着水泥墙、水泥地面，大铁罩子吊灯，黑房顶的装修方案，我就马上会想起艾未未。我老想告诉那些自认为很酷的人，你们太落后了，要知道，12年前的艾未未就已经很冷酷，很简约、非常水泥了。<br />
<br />
　　说到艾未未一不小心打了这么大的一个岔，没办法，只要是提到纽约的事，就不能不说他，有他在纽约，那里就是一个充满刺激和活力的城市。许多年后，我再次回到纽约，那时他已经回到北京，我发现缺少了他的纽约，城市竟变得非常平庸。</div>

							
							
							
							
