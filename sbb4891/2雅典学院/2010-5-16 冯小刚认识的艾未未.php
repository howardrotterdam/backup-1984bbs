																											<div id="postmessage_621349" class="t_msgfont">W=艾未未<br />
<br />
一九八八年夏天头一回到纽约。我们是从伦敦过来的。和垂死的大英帝国相比，这儿有股满不在乎的劲头，北京人叫“愣头青”，但朝气蓬勃。刚下飞机，我表妹的男朋友就开车带我们去兜风，隔东河观望曼哈顿的摩天大楼。那是黄昏时分，灯火通明，气派果然不凡。第二天乘地铁进城，我差点儿被尿骚味熏晕了过去。好不容易爬出地面，灰头土脸。再抬头一看，那些高楼脏兮兮的，压得人喘不过气来，好在天热还能挡挡太阳。<br />
<br />
我们到东村去找W。当年我头一本油印诗集就是他手绘的封面，我和邵飞也是通过他认识的。他八十年代初到美国，在纽约一住七八年，黑了下来。这黑和黑还不一样，有的如履薄冰，有的如鱼得水。要说纽约可比哪儿都改造人。这个当年电影学院动画专业二年级的好学生，整个变了个人：他眼神阴郁，肥头大耳，一身短打扮，满口纽约土话。他走在街上，三教九流都过来打招呼，满脸崇敬。那时东村是流浪汉、酒鬼、毒贩子和艾滋病患者的天下。他哼哈应声，话不多，拍拍这个肩膀，摸摸那个秃头，奇迹一般，那些狂暴的灵魂顿时平静下来。<br />
<br />
他告诉我们，两天后的晚上，穷苦兄弟们要在华盛顿广场示威，反对市政当局驱赶流浪汉的决定，警察肯定会*。他为此花高价买了台可连拍的闪光灯。当马队冲进示威队伍警察抡起警棍那一瞬间，他连续按动快门。照片发在本地报纸上，电视新闻也报道了示威场面，作为目击者他讲述了警察的暴行。尽管头部镜头被遮挡，还是为他捏了把汗，他非法居留，一旦被警察发现肯定会报复。可他满不在乎。<br />
<br />
问他以何维生，答街头画像。说罢他取上画画工具，拦了辆出租车，拉我们到西四街的繁华地段，那儿已有不少中国画家在拉客。可惜那天晚上他运气不佳，等了两个钟头无人问津。有人提议去大西洋城赌一把，他立马收了摊，扬长而去。<br />
<br />
他和艾伦・金斯堡（Allen Ginsberg）混得厮熟，艾伦会很夸张地呼唤他的名字。刚到纽约，艾伦请我们到一家日本馆子吃饭，由他作陪翻译。他用中文拿艾伦开涮，艾伦瞪着眼珠子咧嘴直乐，好像全听懂了。他把警察*的消息告诉艾伦，艾伦立即发表声明。在纽约有各式各样的秘密社会。而W自甘与那些社会边缘人为伍，伸张正义，说明他天生反骨，这大概是他当年加入“星星画会”的内在原因。纽约作为一个相对开放的社会，为其反叛的激情提供了发泄的可能。<br />
<br />
后来听说他回国了，发了财，成了北京的大古董商。这倒也不稀奇，商业化最终会消解一切。而古董市场肯定也是个秘密社会，以他在纽约练就的胆识，足矣。</div>

							
							
							
							
