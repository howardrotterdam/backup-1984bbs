　　古代宰辅是干什么呢？有人形容为：调和鼎镬，折冲樽俎。老子说：治大国如烹小鲜。看来宰辅和厨师确有很多相同之处，这是饮食文化高度发达的中国对治国之术的准确概括。调和矛盾，在今天的中国委实不易。比起霹雳手段、雷霆性格的前任朱F基，温家宝显得温润平和，正如他自己所说的那样：“大家觉得我很温和，但是我认为自己有信念，有主见，敢负责任。”这种性格，和他的南开中学校友周恩来倒是很相似。作总理，这样性格也许更合适。<br />
　　和朱F基相比，两人的性格相差极大，但为什么朱F基一直十分欣赏温家宝呢？除了温出众的协调能力外，外人实在不好有太多的猜测。但历史上伟人将衣钵传给和自己性格、行事方式迥异者的例子屡屡可见。康熙当年以仁慈宽厚治国，但自己已看到这种治国之策带来的种种弊病，选中了不怕麻烦，刚硬严苛的雍正；曾国藩有着传统儒家的拙直，但看到当时中国洋务运动需要通融善变的人，李鸿章成了他着力培养的高足。<br />
　　中国过去的十三年，是劈山开路的十三年，非得有大手笔不可。到今天，我们已经走上了改革开放的不归路，既然加入了WTO，我们照着规则办事就行了。在大的政策方针上萧规曹随并无不当，现在最重要的是解决改革遗留下来的各种矛盾，二十年来，各种矛盾积蓄到今天，已是千头万绪，此时更需要一个剥茧抽丝、协调平衡的高手。这也许是时代选择温家宝的原因吧。<br />
　　对照温家宝和胡锦涛主席的履历，觉得两人身上都有中国传统知识分子宽厚质朴的气质，而且两人的经历那样相似：都是1942年出生，都是在文革前读的大学，而且都是在1968年从北京远赴艰苦偏僻的甘肃，两人从事的职业也都是辛苦奔波的，一个人搞水利，一个人搞地质。温家宝在甘肃地质部门时，全省的沟沟坎坎据说他都踏遍了。而且两人都是1982年调回北京，真正地步入政坛。<br />
　　从24岁到40岁，人生中最美好的16年，他们都扔在了黄河之滨，丝绸路上，这一段经历对他们日后的从政究竟有多大的影响呢？他们相似的经历仅仅是巧合吗？也许他们的经历是对《孟子》中的几句话最好的解释：“故天降大任于斯人也，必先苦其心志，饿其体腹。”今天的记者招待会上温家宝引用了同一篇文章的警句：“生于忧患，死于安乐”。<br />
　　比起新中国前几人领导人，他们没有叱咤风云、战功累累的经历，也没有长于革命家庭的先天优势，他们出自真正的老百姓家庭。是否预示着中国平民政治时代的到来？只有彻底地告别英雄时代，――虽然英雄给我们以人格的美，迎来用制度治国的公民时代，中国才能平稳地往前走。<br />
　　一个温和的、平静的、殷实的中国新时代即将到来，这个时代应该是宪政的时代，是规则的时代。让流血、冲突、暴戾的噩梦远离多灾多难的中国吧，中国的老百姓的期望并不高，小康无非是无衣食之忧，有公民的尊严与权利。<br />
　　我们正在从温饱行走在小康的路上，温家宝，一个平常的名字，当年他作教师的父母给他起这个名字时，和中国大多数老百姓一样，都怀着这样朴素而实在的希望。<br />
　　祝福我们的总理，祝福我们的祖国。</div>

							
							
							
							
