　　对于新当选的国务院总理温家宝，应该说人们对他的了解并不多。虽然他走上政坛已近20年，进入核心部门也有十几年了。但也许因为他行事低调，不喜张扬，人们只能从他偶尔的出镜中，窥见片羽麟爪。<br />
