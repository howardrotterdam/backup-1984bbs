薄瓜瓜，男，1987年12月17日出生。&nbsp; &nbsp; <br />
现为牛津大学一年级本科学生，持中国护照（汉语拼音同名），三年期学生签证。&nbsp; &nbsp; <br />
<br />
2、学习和工作现状：&nbsp; &nbsp; <br />
自2006年10月，就读于英国牛津大学贝利奥学院经济、哲学、政治三项综合专业本科，为全额奖学金住校学生。&nbsp; &nbsp; <br />
主要社会职务：&nbsp; &nbsp; <br />
牛津大学最高联合总会常务理事&nbsp; &nbsp; <br />
牛津扶助海外贫困学生协会主席助理&nbsp; &nbsp; <br />
2008北京奥运会海外学生志愿团团长&nbsp; &nbsp; <br />
英国亚当史密斯经济研究会会员&nbsp; &nbsp; <br />
牛津大学政、经、哲协会发言人&nbsp; &nbsp; <br />
<br />
3、主要经历&nbsp; &nbsp; <br />
薄瓜瓜从小学到初一在北京景山学校读书，一直连任中队长。初中时报考了英国哈罗公学，在预考中获得名额后，经全英公立学校统考，被该校录取，成为历史上第一位中国学生。后因成绩突出授其奖学金，并将他的学习笔记印发为范本。17岁时国外出版了他的英文专著《还有不同》，该书主题是反对盲目追赶时尚，已被新加坡出版社译为中文，正在征求本人同意出版。&nbsp; &nbsp; <br />
<br />
2005年底薄瓜瓜参加英国统一高考，报了竞争最激烈的英国牛津大学经济、哲学、政治三项综合专业的创史学院，经全国统考和本校复考及再淘汰面视等程序，薄瓜瓜名列前茅，成为该学院本专业的首位中国学生，并获得全额奖学金。2006年10月正式入学后，在牛津最高联合总会换届选举时，当选为常务理事，也是首位进入该会领导机构的中国人。&nbsp; &nbsp; <br />
全球亚洲协会今年将他评选为38名杰出华人学生之一，在北京市教委等单位举办的专题活动中，薄瓜瓜作为“景山学校杰出校友”，被大会推选为主题发言人。&nbsp; &nbsp; <br />
<br />
