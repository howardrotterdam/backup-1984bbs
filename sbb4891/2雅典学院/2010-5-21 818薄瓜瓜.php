<br />
薄瓜瓜的假期活动一般非常简单，每一个假期他都回家度过，基本活动是照看老人、陪伴父母，参加专业文体训练，补修中国文化课程，或到贫困地区体验生活做采访笔记。&nbsp; &nbsp; <br />
<br />
