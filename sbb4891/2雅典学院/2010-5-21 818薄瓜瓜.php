认识薄瓜瓜的人基本上公认他为人友善，喜欢助人，虽然个性活跃，但从小就懂得注意影响，注重自己的形象，交友谨慎。他的社交圈子只限于寄宿学校范围。薄瓜瓜在去英国读书之前和姥爷、姥姥生活在一起，除了家庭亲友和学校的老师同学，他和社会上的人没有来往。在英国读书期间，他完全生活在封闭严格、没有周未的公立男校中。除了参加正规的社会公益和学术活动外，薄瓜瓜没有其他的社会联系。&nbsp; &nbsp; <br />
<br />
而校园里的薄瓜瓜却十分活跃。比如，在哈罗公学期间，他报名参加了英国社会在学生中常年组织的“攻击性淘汰赛”，有点类似国内的“铁人三项淘汰赛”。这是在全英所有公立学校开展的一项活动，要进行长达三年的残酷训练和淘汰，包括要过生死关、饥饿关、险情应变等等，由英国皇家海军陆战队特种兵军官担任教官，一轮一轮地淘汰。薄瓜瓜参加的这一期比赛最后坚持下来的只有8名学生，他是其中惟一的东方人。&nbsp; &nbsp; <br />
<br />
