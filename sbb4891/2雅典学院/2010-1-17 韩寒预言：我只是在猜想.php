2011年1月&nbsp; &nbsp;政府新一轮经济刺激计划中拨款1000亿用于网络评论员，网络评论员的薪水由每条五毛涨为每条一元，2011年的目标是有1000亿条正面评论，坚守在评论征地的五毛党们黯然落泪，苦撑多年，被骂无数，终于等来了大部队。从此，在各大互联网评论中，网评员和正常人类的比例达到5比5。<br />
<br />
2011年&nbsp; &nbsp;&nbsp; &nbsp; GOOGLE，YOUTUBE，FACEBOOK，TWITTER等网站宣布重返中国并开放注册。<br />
<br />
同时&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;所有上述网站在美国宣布并未在中国开展任何业务。<br />
<br />
马上&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;这条消息被全新长城系统屏蔽，在国内无人知晓。<br />
<br />
2011年&nbsp; &nbsp;&nbsp; &nbsp; 在上述网站注册的用户被长城系统追踪，他们的电脑被锁定，综合类网站只能打开人民网，新华网，论坛类网站只能打开强国论坛和铁血论坛，视频类网站只能打开央视一套。经过系统重装，都不能破解锁定。<br />
<br />
2011年&nbsp; &nbsp;&nbsp; &nbsp; 电脑涨价一倍，出现卖电脑的黄牛。但用户买回新电脑以后发现还是只能打开上述规定网站。<br />
<br />
2011年&nbsp; &nbsp;&nbsp; &nbsp; 房地产涨价一倍，在房地产交易网上，房源必须标明是上网房和限网房。<br />
<br />
2011年&nbsp; &nbsp;&nbsp; &nbsp; 南方都市报，南方周末被改名为男方都市报和男方周末，被重新转型为婚姻介绍类报纸。同时，网评员和正常人类的比例达到9比1。<br />
<br />
2011年&nbsp; &nbsp;&nbsp; &nbsp; 某市领导被人肉搜索。<br />
<br />
2011年&nbsp; &nbsp;&nbsp; &nbsp; 互联网第二轮整治，所有搜索网站被关闭，各大门户网站和报纸发表“搜索，让我们变成懒人”、“网络搜索严重影响小学生的动脑动手能力”等文章，领导们表示，我们从来不用网络搜索，也到了今天的地位，说明网络搜索百害而无一利。<br />
<br />
2011年&nbsp; &nbsp;&nbsp; &nbsp; 百度被百事收购，成为饮料官方网站。<br />
<br />
2012年&nbsp; &nbsp;&nbsp; &nbsp; 新浪网曝光某村领导收受贿赂500元，该新闻被评为2012中国年度新闻，该新闻点击量达到5000亿，很多人反复点击，经过删除后留言量高达一百万条，很多人认为，这是舆论监督重新开始的一个新起点。但是网络投票中，九成网友认为该新闻不应该发表，因为破坏了社会的稳定，甚至可能导致民族的分裂。<br />
<br />
2012年&nbsp; &nbsp;&nbsp; &nbsp; 新浪网被小学生举报搜索到有人激凸。新浪网被停业整治。<br />
<br />
2013年&nbsp; &nbsp;&nbsp; &nbsp; 所有论坛停止注册和发帖功能，中国作家协会和中国文联接收互联网，成为互联网内容提供商。互联网从WEB 3.0退回到WEB 0.3，网络模式改为只能浏览资讯，不能发帖。<br />
<br />
2013年&nbsp; &nbsp;&nbsp; &nbsp;小学生健康成长委员会举报新浪网又出现激凸。<br />
<br />
2014年&nbsp; &nbsp;&nbsp; &nbsp;新浪被激浪收购，成为饮料官方网站。<br />
<br />
2014年&nbsp; &nbsp;&nbsp; &nbsp;小学生健康成长委员会举报，有人在QQ上向他们发送露点照片，导致该学生看到以后上街强奸了数十人。<br />
<br />
2014年&nbsp; &nbsp;&nbsp; &nbsp;QQ，MSN等聊天工具被禁止，个人博克和微博被禁止，但是屡屡有不法分子通过各种技术手段上传一些私人的想法和信息。政府非常头疼。<br />
<br />
2015年&nbsp; &nbsp;&nbsp; &nbsp;政府部门将网络全部切断，并统一推出上网专用电脑，只有用该电脑才能上网，没有键盘的接口，只给你一个鼠标。改专项整治活动的口号为“只给你丫一鼠标，看你还能怎么着”。<br />
<br />
2016年&nbsp; &nbsp;　中国网民下降到100万，所有网站合并为一个网站，输入任何网址都将链接入该网站。该网址更新内容以当天人民日报为准。同年，中国的互联网产业消失，直接导致近500万互联网相关产业从业人员失业，除了因电子邮件被取消后重新兴起的邮政行业接纳了十万人以外，490万人面临再就业困难。同时，近100万五毛党失业。五毛党感叹道，做牛做马做狗了半辈子，连退休金都没有。<br />
<br />
2016年&nbsp; &nbsp;人民日报发文：牺牲了一个产业，换来了国家的稳定，值。<br />
<br />
2016年&nbsp; &nbsp;新闻联播评论，互联网产业发展下去，国家将面临分裂的危险，国外反华势力和国内分裂势力借助着互联网煽动群众，幸亏有关部门采取强硬手段，避免了事态的扩大。<br />
<br />
2016年&nbsp; &nbsp;周久耕平反。周出狱后任命信息产业部部长。余秋雨被任命为文化部部长。<br />
<br />
2016年&nbsp; &nbsp;100万失业的五毛党因为根本没有其他技能，一直找不到工作，没有生活保障，数万五毛党开始向北京聚集，该年儿童节，十万五毛党在政府门前静坐绝食，要求政府安排工作或者买断工龄，并追加公务员称号。人民日报发文表示，政府从来没有过网络评论员这个职业，所有的网络评论均为五毛党自发评论。五毛党们拿不出任何劳动合同来证明其和政府之间的劳动关系。<br />
<br />
五毛党的发言人表示，他们当年是地下工作者，为国家的稳定立下了汗马功劳。<br />
<br />
政府表示，限五毛党三日内离京，否则将予以逮捕。人民日报发文表示，赞美政府就不应该向政府施加压力。赞美政府不能作为换取钱财的条件。赞美都应该是无偿的。<br />
<br />
2016年&nbsp; &nbsp; 五毛党继续绝食，市民们表示，支持五毛党绝食，绝不向五毛党提供食物，并且监督五毛党绝食，切断一切五毛党可以获得食物的途径。<br />
<br />
第二天，文化部部长余秋雨在绝食现场发表“含泪劝五毛书”。五毛党们纷纷表示，讲的太深奥，听不懂啥意思。<br />
<br />
2016年&nbsp; &nbsp; 五毛党的罪名包括非法集党，非法游行，非法示威，攻击政府，暴力抗法，诽谤政府，危害公共安全，扰乱社会秩序，随地吐痰等。组织者被逮捕，但政府表示，宽容对待不明真相的盲从者，给他们一个改过自新的机会，给绝食者每人发五毛用于充饥。新闻联播评论这一场景：五毛见五毛，两眼泪汪汪。<br />
<br />
2016年&nbsp; &nbsp; 五毛危机平安解决，再现党和政府稳定局面的能力。为首的五毛党面对央视记者的采访，说了三个字，悔当初。<br />
<br />
2017年&nbsp; &nbsp; 失业人数剧增，互联网产业的崩溃导致经济严重衰退，政府表示，制造业将再一次作为国家支柱产业，中国应当利用一切自然的资源，以低价的优势向海外出口。<br />
<br />
2017年&nbsp; &nbsp; 海外反华势力勾结起来，狼狈为奸，蛊惑联合国以及各国，通过了关于各国家禁止向中国进口任何商品的法案，以反对中国对互联网进行封锁。中国政府强烈谴责，表示中国的互联网管理是中国的内政，其他国家无权干涉。其他国家表示不向中国进口商品是该国的内政，中国政府无权干涉。<br />
<br />
2019年&nbsp; &nbsp;&nbsp;&nbsp;国庆70周年大阅兵，在阅兵当天，政府表示，闭关锁国，苦练内功，让一切反动势力在我们面前颤抖吧。当天，中国向全世界发出电文，电文内容共八个字：三天不打，上房揭瓦。很多国家表示难以翻译。<br />
<br />
