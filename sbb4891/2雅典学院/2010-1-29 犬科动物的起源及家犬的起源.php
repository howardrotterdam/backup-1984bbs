													<h2>犬科动物的起源及家犬的起源</h2>
						
						
																											<div id="postmessage_457762" class="t_msgfont">发表于：《平安阿福流浪动物救助会》<br />
<br />
一、犬科动物<br />
家犬与狼、狐、郊狼、豺和野生猎狗等相似的动物属于同一科。他们共同的名字叫犬科。这些动物都有共同之处，最突出的就是都有必不可少且适应性很强的牙齿。早在人类开始饲养狗之前，犬科动物的祖先就在进化了。家犬的确切起源虽还不确定，但这种进化过程却提供了一项迷人的研究课题，有助于我们深入理解狗的生理结构和性情。今天人类对狗的驯养已产生了惊人的多样化。<br />
二、犬科动物从何而来？<br />
古新世时期<br />
<br />
　　6000万年以前，在森林中有一种体型小，貌似鼬鼠的动物，它的身体长而柔韧，长尾，短腿，名为细齿兽，它不仅是犬科，同时还是其他一些如浣熊、熊、鼬鼠、麝猫、鬃狗和猫科动物的早期祖先。它像现代的熊一样用脚底爬行，不像现代的狗那样用“趾”走路。脚有五个分离的脚趾，牙齿是典型的食肉动物的牙齿。大脑很小，但比同时期其他原始食肉动物来得大。<br />
<br />
渐新世时期<br />
<br />
　　到了渐新世的早期，大约在3500万年前，细齿兽已经进化成多种早期的犬科动物。科学上查知，有40多种原始犬科动物，一些是类熊狗、一些是类鬃狗，最令人惊奇的是类猫狗。另外，还有一种类似现代狼的狗，也只有这种狗得以生息繁衍至今。<br />
<br />
第三世中新世时期<br />
<br />
　　到第三世中新世的早期，大约在2000万年以前，一种很基本而类似现代狗的狗成出现了，称之为Mesocyon。它的颌比现代狗短，有修长的身躯和尾巴，粗短的腿。后脚仍是分开的五个脚趾，不像现代犬科动物那样为并拢的四个脚趾。到第三世中新世的末期，在1000-1500万年前，发现了一种有较长颌和较大脑容量的犬科动物Tomarctus的化石。尽管它的智力不如狗，但已具备了狗群居的全部本能。<br />
<br />
上新世时期<br />
<br />
　　真正的犬科动物首次出现在500-700万年前。它开始用四个脚趾行走（第五个脚趾逐渐退化成了残留趾），并且趾间比较靠紧，这种构造很适合捕猎。<br />
<br />
<br />
二、家犬的进化<br />
狗是人类最忠心的朋友，那我们什么时候和它们结交的？分子演化的研究显示不管是吉娃娃还是圣伯纳，全世界的家犬都是演化自一万五千年前东亚的狼。1997年，经过对140种狗、162种狼、5种北美小狼及12种豺的DNA进行比对，研究人员表明，只有狼才是狗的祖先。<br />
家犬（Canis familiaris）最早出现於东亚，从野狼（Canis lupus）驯养而来，然后传播到全亚洲和欧洲，之后随着人类在一万四千至一万两千万年前横越白令海峡到了新世界。<br />
人类在许多方面改变了狗，但一些科学研究人员也认为，狗也改变了我们。在我们从猎人变成畜牧者并最终变成城市居民的迁移过程中，它们守护着我们，为我们寻找食物，帮我们排谴孤独的感觉。<br />
15000年前：亚洲某部落驯养的首批狼，为家犬的前身。 <br />
14000年前：家犬随人类迁移到世界各地，包括欧洲、非洲等。 <br />
12000-14000年前：狗只散布至澳洲。 <br />
12000年前：狗只在美洲和以色列出现。<br />
<br />
三、家犬起源于何处？<br />
所有的狗都能上溯到狼，但新的研究提出， <br />
&nbsp; &nbsp; 狗最早成为人类的朋友是在中国 <br />
&nbsp; &nbsp; 德博拉．史密斯 <br />
&nbsp; &nbsp; 摘自The Sydney Morning Herald <br />
我们永远无法确切知道它们的历史是如何开始的。也许，世界上最早的一些爱狗人士喂养了一些可爱的狼崽，或者，更切合实际一点，他们驯服了一些野生的成年狼来帮助狩猎。 也有可能，一些胆子较大的在营火周围觅食腐肉的狼，最后成为了家庭的一分子。 这一事件在人类及其最好朋友的进化过程中都具有重大意义，有关这一事件发生的时间和地点，我们已经缩小了范围。 <br />
<br />
瑞典科学家在分析全球逾500种狗只毛发样本后，发现所有狗只几乎都有着相同的基因库，而其中东亚狗只的基因变异较多，故相信狗的祖先，很有可能源于东亚。这个研究结果已于《科学》杂志上发表。研究人员指出，约在一万五千年前，居于中国或附近的人类将野狼驯养成家犬，它们就是家犬的始祖了。后来随着人类迁移，家犬被带到欧洲，而在一万二千至一万四千年间，再由猎人从白令海峡带到北美洲去，辗转再带到南美洲乃至世界各地，从此家犬便遍布于全世界，并繁殖出不同的品种。根据最新的基因研究结果，所有狗只，包括美洲的纽芬兰犬，甚至爱斯基摩犬，都是亚洲狼的后代。<br />
<br />
四、家犬和狼的区别<br />
不管人类和狗结交了多久，哈佛大学的生物人类学家Brian Hare等人在同一期Science也发表了一篇文章，他们发现狗找到人类暗示的隐藏食物之能力比黑猩猩和狼还好。不管多大的小狗都能很好地依人类神情所指示找到食物，即使它们只有微不足道的经验。黑猩猩是人类的近亲，但它们和人类的沟通能力连狗都不如。狗在从狼变成狗的过程中，遗传基因已经打上人的烙印。狗是因为人才成狗。<br />
狼和狗的幼犬在外观上很难分辨。从人类角度看，普通人认为狗和狼最大的区别在于尾巴是否翘起。这是不全面的判断，经过动物学家的研究，狼和狗表达情感的方式是相同的。也会翘起尾巴，愉快的时候会摇尾。并不是狗在被人类驯化的过程中学会了表达情感的方式，而是来自于祖先狼。不同之处是狼通过对狼群中的头狼摇尾的方式传达情感，而狗对人类摇尾。可以说，狗也进化的过程中把主人当作了狼群中的首领。狗和狼最大的不同是对待人类的态度。关在牢笼之中的狼和被遗弃的狗同样可怜。</div>

							
							
							
							
