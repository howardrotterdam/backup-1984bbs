<br />
下面给个大致的制度脉络<br />
周代：君主共和制（周王充当诸侯的仲裁者。某个王不义时，大家就把他换了，由大臣和国人共治，史称“国人共和”）<br />
春秋战国：君主共和制==〉君主专制（各国变法，主要是军国主义，不这么干的都被兼并。这和二战英美也搞战时政策差不多）<br />
秦到宋：君主专制==〉君主共和制 （战争结束了，秦朝还在搞战时政策，很快灭亡。之后，法家和儒家相互抗衡，到赵匡胤时彻底明白了专制并不能保全赵家，只有适度放权，各阶层共和，才能避免五代十国变六代十国）<br />
元明清：君主专制<br />
朝：前三十年搞民粹极权、后三十年搞官僚集权。</div>

							
							
							
							
