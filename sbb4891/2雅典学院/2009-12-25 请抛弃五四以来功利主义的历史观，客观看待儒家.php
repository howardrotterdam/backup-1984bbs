																											<div id="postmessage_395529" class="t_msgfont">　　儒家的基本思想，是尊重王权。为什么要尊重王权呢？从当时的历史背景上看，其实也没别的选择。所有情况，大概就是如下几种：<br />
　　一、如果以周王为王权...虽然已经没有正面发展的可能了，但却提供了一个负面的教训：诸侯分立，不尊王权，就会发生混战。以下都以诸侯王为王权说事。<br />
　　二、鲁国就有沦为周朝的可能：显贵自建城堡，划地而守。这是孔子坚决反对的。拆城堡，是孔子从政期间所主要想干成的事。<br />
　　三、大夫架空君主，这也是孔子所反对的。理由在当时的历史背景下再清楚不过：这么干没好下场。因为根据法律和惯例君主应该掌权，大夫非要夺权，虽然可能一时轻易得手，最后几乎一定会以火并告终。<br />
　　四、共和执政。共和执政如果行得通，当时怎么就没行下去呢？呵呵。其实这是一种最坏的模式：一个国家，没有一个名正言顺的元首类长官是不行的，从古至今都不行。而当时要是弄一个非周王的元首出来...估计执政者们也料到除了名不正言不顺以外，没有区别。所以没戏。毕竟他们没有民主制度做保障。<br />
　　五、君主共和。君主共和有两方面的坏处。之一是它和”三“很像，完全可能转化成“三”。之二是在一个国家不能实施民主制度的情况下由贵族掌权*，这是最坏的模式之一，既方便贵族对人民残渔酷取，又使国家在战争时期失去核心，波兰就是这么灭亡的。大家可能会想到大宪章时期的英国也是这个。这就得展开说了。<br />
<br />
　　英国的大宪章，是在西方文明发展的后期，在很浓重的历史积淀下形成的。西方民主和法律传统都远比中国优越。因此，大宪政是“限制君主权力的宪章”，而不是“民主好处多”之类思想的体现。大宪章的宗旨是为了防止最坏，而不是为了追求最好，而且细节上有可操作性。**<br />
　　显然，古代的儒家根本理解不到这个层次。儒家限制君主权力的模式，仍然受“汤武革命”的直接影响。不是从细节上以法律规范君主的行为模式（那样就不符合“礼”的要求了），而是要等到君主让人忍无可忍之后，再把他给革掉。<br />
<br />
　　结论：如果说儒家支持君主专制，意思是说儒家把君主地位抬得太高，无论是理论上还是实质上都是如此；而不能说儒家就无条件遵从君主，反对革命。这就如同某个政治派别主张赋予美国总统更多权力一样。<br />
　　但是，古代儒家不仅对民主法制的基本原则都没有正确认识，对制衡制度也没有深入研究。除非君主愿意虚心纳谏，否则儒家在限制君主方面就没什么好办法（死谏，苦等主子恩赐等等，当然不是好办法）。自有儒家以后，很难说哪次废立是真正依据儒家思路来进行的，都可以算沾边，但实际上也都是乱来的。儒家更无从以人民起义之类的方式来实践其限制君主的理念。最后，儒家也只能满足于事后加以解释，实际意义就很小了。<br />
<br />
*：贵族制+民主制是希腊-罗马民主时期的常态之一。<br />
**：英国和波兰的区别，除了细节不同以外，还在于英国作为一个岛国，又是相对的强国，不必害怕由于权力分散而无法应付战争局面的问题。</div>

							
							
							
							
