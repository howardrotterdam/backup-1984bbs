君主共和制，是君主主导的社会各阶层共和，这需要君主有一定的权威，还要一定的强制执行力（1千里直接控制区为力量基础），并且维持一种相对中立的态度。中庸，就是在各阶层的利益表述中，维持一个不偏的态度。<br />
