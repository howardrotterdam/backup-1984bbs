																											<div id="postmessage_395458" class="t_msgfont">秋风：无法专制的周王<br />
2009-12-19&nbsp;&nbsp;华商报<br />
<br />
　　按照古人的说法，周、春秋时期为封建制，而权力的分散乃是封建秩序的基本特征。近人瞿同祖先生在其名著 《中国封建社会》中有很具体的描述： “从横的方面看，分化是封建政治的一大特点。我们都晓得天子虽拥有天下的土地，但他不将它们都握在一人的手掌里，而是将它们分赐给许多同姓异姓的贵族。这些贵族各自治理其土地，便成为那里的主人，食其地之所入，役其人民，有自由处置的权力，用不着问命于中央，中央也不过问这些琐事。”而在封建时代，土地与人直接联系在一起的，或者说，人是从属于土地的，因此，对土地的控制权的分散，也意味着对人的控制权的分散。<br />
<br />
　　这种权力的分化，首先存在于周王与诸侯之间。今天人们常引用《诗经》的一句话：“普天之下，莫非王土;率土之滨，莫非王臣。”但这种说法只在政治上成立，而不是法律用语，就仿佛“人民主权”只是一个政治论断而不是法律事实一样。<br />
<br />
　　在法律上，周王首先是一个封建领主，在日常生活中他也就是一个领主，最初还是最大的领主，后来连这一点都做不到了。在这里需要辨析的 “天子”一词，从理论上说，当时的周王不可能想到自己是天子，这是后人的附会。西欧中世纪历史上同样只有“国王”而没有“皇帝”，神圣罗马帝国皇帝的头衔是从罗马延续下来的。<br />
<br />
　　周王有自己的封建领地，这就是史书所说的“王畿”。按照并不那么可靠的《礼记王制》的记载，王畿不过方一千里。只有这块土地是完全属于周王，但是，周王仍会在王畿上进行再次分封，受封者对国王负有役务。周王派遣家臣直接管理直属自己的庄园，这些庄园上的产出供王室之用。<br />
<br />
　　出了王畿，便是诸侯的封地，诸侯自行治理。周天子对诸侯确实享有一定权利，诸侯对天子负有一定义务，主要的义务是向周王提供役务，这包括日常在王廷随侍周王，向周王提供建议和服务，在战时带领自己领地上的武士随周王出征。<br />
<br />
　　因此，在周代――― 更不要说到了春秋时代，周王绝不是后来那个高高在上的皇帝。当战国之世，有弟子向孟子请教周代的爵位制度，孟子回答说：天子一位，公一位，侯一位，伯一位，子男同一位，凡五等也(《孟子万章下》)。<br />
<br />
　　这里，公、侯、伯、子男四等爵的说法大约属于想象。诸侯有大小之分，但假如他们直接受封于周王，那就属于周王的“直接封臣”，法律权利是平等的。至于天子也不过是一个爵位之说，则道出了历史真相。在王权专制主义正在崛起的时候，孟子从理论上阐述这一点，则具有明显的宪政主义诉求。他是在宣称一种政治理想，他理想中的天子不是绝对的，而只是最大的领主，他与其他领主确实只是“同侪”。<br />
<br />
　　孟子的这种说法后来不断产生回响。这甚至已经成为一条宪法。汉代经学非常发达，经学甚至就是宪法学。为统一宪法学说，西汉宣帝曾召开石渠阁会议，东汉章帝又召开白虎观会议，召集儒生讨论经义，最终形成《白虎通义》。这本书具有宪法性质，而它开篇即称：“天子者，爵称也。”在汉代的儒生看来，汉家天子其实并没有绝对的地位和权力。这确实是一个高尚的岗位，却不是神圣的。汉儒不承认皇帝的绝对统治权。<br />
<br />
　　到了明末，顾炎武再度提起这种主张，他在《日知录》中说：“为民而立之君，故班爵之意，天子与公侯伯子男一也，而非绝世之贵。代耕而赋之禄，故班禄之意，君卿大夫士与庶人在官一也，而非无事之食。是故知天子一位之义，则不敢肆于民上以自尊;知禄以代更之义义，则不敢厚取于民以自奉。不明乎此，而侮夺人之君，常多于三代一下矣。”顾炎武清楚说明了天子一爵说的政治用意。而假如天子都不再享有绝对的统治权，那整个政府当然也不享有这种权力。<br />
<br />
　　由此可以看出，明智、民主的政治哲学家总能从封建制中得到很多有益教诲，因为那个时代确实不存在后来人们在王权制下看到的暴虐的专制，封建制中包含了诸多宪政的元素，比如分权、自治、法治、个人权利等等。英格兰的宪政主义革命，就是以封建的名义发动的。</div>

							
							
							
							
