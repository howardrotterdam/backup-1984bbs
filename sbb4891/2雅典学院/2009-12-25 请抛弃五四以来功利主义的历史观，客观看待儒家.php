																											<div id="postmessage_395346" class="t_msgfont">支持写实这次说的。<br />
儒家支持君主专制是毋庸置疑的。这也为什么会有“外儒内法”这个荒谬说法的原因：因为法家支持君主专制，儒家也支持，所以，喜欢强调法家的人就把儒家这个部分瞎算给法家了。<br />
不过，儒家虽然支持君主专制，但是不等于支持这种专制自上而下延续到每一个人头上。因为只要智力较高的人（可惜法家不是这类人）都可以看出：当权力往下延伸得越来越远以后，虽然它名义上还是君主的权力，但是却只能沦为官僚的特权。<br />
因此，儒家宁可依靠一种比较松散的国家结构来实现君主的专制，也就是说：既然君主无论如何也不可能自己管理人民，那么宁可更多地以道德来规范人民，而不是完全用有名无实的君主权力。<br />
也就是说：儒家对于靠近中央的地方和靠近人民的地方，采取的是不同的策略。在中央附近，儒家反对大夫掌控国政（这也是孔子从政期间主要干的事）；在人民附近，则反对以严厉的法律来直接控制人民。</div>

							
							
							
							
