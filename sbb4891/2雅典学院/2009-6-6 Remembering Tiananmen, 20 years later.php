Remembering Tiananmen, 20 years later<br />
Yesterday, June 4th, 2009, marked the 20th anniversary of the military crackdown on student protesters gathered in Tiananmen Square in Beijing, China. Beginning in April of 1989, thousands of students and other citizens started gathering in groups large and small, protesting many issues, centered on a desire for freedom and democratic reform. By mid-May of 1989, hundreds of thousands of protesters occupied the square, staging hunger strikes, and asking for dialogue. Chinese authorities responded with a declaration of martial law, and sent soldiers and tanks from the People's Liberation Army, preparing to disperse the crowds. Late on June 3rd, 1989, the tanks and armored personnel carriers rolled into the square, killing and wounding many, mostly civilians - estimates vary widely, from several hundred to several thousand dead. The first 17 photos below were taken in 1989, the rest are from this year, as people remember the events, the ideals, and the fallout from that fateful day. (32 photos total) <br />
