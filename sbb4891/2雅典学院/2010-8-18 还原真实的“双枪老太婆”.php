													<h2>还原真实的“双枪老太婆”</h2>
						
						
																											<div id="postmessage_787819" class="t_msgfont">抗日游击队之母――洪文国<br />
<br />
&nbsp; &nbsp;有许多人被后人称之为抗日时期的双枪老太婆，但仔细推敲其年龄，抗日时期还是个不知丑俊的少女，何以被时人称之为老太婆？不免疑惑抗日战争时期是否有这样一个真实人物。查阅史料，方知抗日战争时期真有一位老太太，时年已六十余，被称为游击队之母，又有称之为游击队之祖母，当时影响很大，民间称之为双枪老太婆。<br />
<br />
　　赵洪文国（1881―1950），女，满族，夫姓赵，本名洪文国，辽宁省岫岩县哨子河乡红旗沟人，抗日英雄赵侗之母，人称“赵老太”，无党无派。抗日战争时期她屡挫屡战，参与组织创建了辽南“少年铁血军”、河北“国民抗日军”、河南“太行山光/复军”以及“晋察冀游击纵队”，被誉为“游击队之母”“民族之母”，民间称之为“双枪老太婆”，在海内外享有很高的声誉。<br />
<br />
　　赵老太太，14岁目睹了日本侵犯东三省，23岁亲历日俄战争，因而她自幼对侵略者怀有仇恨心里。于是，在家业兴旺后，1929年开始筹办小学，试图让家乡孩子摘掉文盲的帽子，做一个有知识，有文化，有气节的人，以实现富国强民的理想，免受外夷欺凌。<br />
<br />
　　1932年春，九一八事变后，赵侗由北平回故乡组织抗日义勇军。此时赵老太太虽年近花甲，但爱国激情不减，不仅变卖家产充作抗日经费，还带领全家为义勇军服务，将自己的家成为抗日军的指挥所和交通站。同时恢复战乱解散的赵家堡子小学，在三子赵侗的帮助下，坚持教授国语的教材，抵制日本奴化教育，教育少年儿童热爱祖国，决不去做亡国奴，使赵家堡子小学的师生成为抗日的先锋。不久苗可秀于家乡本溪安排好亲人后，亦来到赵老太太家中，与赵侗共同参加邓铁梅组织抗日武装。<br />
<br />
　　1933年白君实、刘壮飞等来到赵家，在赵老太太和二儿子赵连俊的帮助下于，组建抗日别动队，赵侗、赵伟离开邓部，正式成立“别动队”。翌年苗可秀离开邓部返回赵家，根据抗日形势的需要，共同协商将“别动队”改组，于同年2月成立“少年铁血军”。赵老太太，负责购买保存枪支弹药，安排铁血军的重要军事会议，收集日伪情报等等。很快的队伍发展到基本部队500名，其中大学生占十分之二，师中学生占十分之五，小学生占十分之二，农民占十分之一。因此被当地百姓称为学生军，“用黑铁赤血之精神，采全民革命之手段，收复东北，振兴中国。”<br />
<br />
　　1934年2月，日军得知赵家为少年铁血军中枢，将赵家16间房屋全部焚烧，30年积累的家产毁于一旦，但赵老太太并未屈服，同年3月赴大孤山等地购买印刷机，在自己筹办的赵家堡子小学组织学生印刷抗日宣传品，然后与儿媳王全一等，潜入凤城、岫岩、庄河、孤山等县城，以少年铁血军的名义张贴散发，一时民气大作，日伪甚为惶恐。<br />
<br />
　　1934年8月，日寇再次突袭赵家堡子，将赵老太太一家六口抓进监牢，株连的赵氏家族和乡亲300多人。赵老太太面对日寇的威逼利诱，始终坚贞不屈。两个月后，敌人无奈，报请关东军司令部拟秘密执行死刑。临刑前赵老太太在铁血军的配合下机智假释出狱，并解救出株连乡亲。随后找老太太携年迈丈夫及年幼子女潜往北平，在东北救国会的帮助下，筹集军火，联络抗日义士，支援少年铁血军。<br />
<br />
　　1935年12月，铁血军由辽南一隅，渐次发展到辽东、热河各地，队伍发展到11个方面军11000勇士。赵侗以铁血军领导人为班底，成立了中国第一民众抗日自救政府“辽南临时政府”，公选铁血军总司令赵侗兼任政府总裁。概略计算，辽南少年铁血军各路连年大小战役300余次，杀敌在4000余，俘日伪军警2000余名。<br />
<br />
　　1936年5月，为壮大铁血军，增强抗敌力量，赵老太太的帮助赴北平的赵侗争取关内各界爱国团体和人士支援，筹集武器、弹药，由海道运往东北。然而，由于日寇封锁进入东北水陆交通，加之当时全民抗战尚未开始，筹集军火的计划多次受挫。<br />
<br />
　　1937年1月，赵老太太与赵侗和战友得知日寇准备进犯热河，侵占河北，决定在北平组织武装起义，抗击日寇。义举得到杜月笙、史笙赓、沈鸿烈、马占山、张学良诸位先生的热心捐助，以及东北救亡总会的支持。随后从东北调来的优秀军事干部汇聚赵老太太北平寓所，分布到晋察冀各地进行秘密活动，考察地形，调查民枪，联络平西民团，发展群众基础。初步计划起义部队由河北向热河推进，杀回家乡与少年铁血军会师。赵老太太开始了紧张的购买运输军火，招募运送游击队员等等准备工作。<br />
<br />
　　1937年7月20日，赵老太太与赵侗和战友24人在北平临时寓所焚香发誓，而后分两路于深夜出城，在城郊西南白羊城起义，组织建立“华北国民抗日军”。打响了华北民众抗日的第一枪，攻破北平第二模范监狱，解救包括著名共产党领导人在内的千余名犯人，在全国首次击落日机一架，激起国内外震动。队伍迅速发展成为拥有基本部队有9个支队，外围6个支队的游击队伍，分别驻防在平郊、灵源、怀涞、热河等地，总共计25000人左右。八路集团军总司令朱德和副总司令彭德怀亲自书信邀请，将国民抗日军收编为第八路集团军晋察冀军区第五支队，赵侗任司令。<br />
<br />
　　1937年9月，赵老太太南下武汉，一面宣传抗战，争取后方各界给予军火支援，一面率领东北青年和两个女儿去河南太行山开辟新的抗日根据地，筹建抗日光复军，当时太行山百姓响应者上万之众。经过整编后，赵老太太派遣光复军先遣队北上，准备打通敌后通道，与河北国民抗日军和东北少年铁血军联络，形成贯穿南北的敌后游击战线。<br />
<br />
　　“保卫大武汉”非常时期，赵老太太发表《对于目前局势的几点意见》，奔走呼号全民抗战，身体力行收复国土，引起国内外重视。国际著名记者斯诺、爱泼斯坦以及反战同盟成员绿川英子等多次采访赵洪文国，合影留念并多次发表文章，向国际反法西斯阵营宣传赵老太太，专门介绍其抗战事迹。其中爱泼斯坦在1991年出版的《人民之战中》再次披露了赵洪文国为游击队购买运输军火和赵侗将军在河北组建抗日游击队的历史。<br />
<br />
　　1938年9月间，赵老太太应邀去广州、香港、南洋各地，从事抗日宣传和募捐活动，受到著名爱国实业家陈嘉庚等南亚华侨的热烈欢迎，为抗日募集巨额经费和大批物资。在香港与宋庆龄、何香凝的妇女界名流组织火炬游/行，宣传抗/战，激发了海外华侨的抗日爱国热情，其场面壮观感人。<br />
<br />
　　国民政府为表彰赵老太太为创建抗日游击队做出的贡献，尊她为：“游击队之母”。一些报刊撰文说，蒋介石与腰插双枪的赵老太太合影留念，并且结拜为兄妹。蒋夫人宋美龄在三八妇女节发表文章，称赵老太太为“全国妇女之楷模”“民族的母亲”，把她比作孟母、岳母。宋庆龄、何香凝、宋霭龄、朱光珍、史良等妇女界名流也纷纷题词赞赵老太太为：“女中模范。”“母教救国。”“求中华民族解放大道自由平等地位，赵老太太顽强抗敌的精神做领导先锋。”<br />
<br />
　　冯玉祥将军与赵老太太成为挚友，赞她是“女中豪杰”，“句句英雄话，愧死男儿汉。”爱国人士陶行知先生和著名作家冰心女士共同编著了《赵老太太》一书，并写诗赞道：“东洋出妖怪，中国出老太。老太捉妖怪，妖怪都吓坏！说起赵老太，谁个不崇拜？要想中国好，学学赵老太！”<br />
<br />
&nbsp; &nbsp;<br />
著名作家茅盾在赠送赵老太太的条幅上抒发感慨：“是新中国贤母的典型，亦是新中国新女性的良师，耽于享乐的后生对之应当愧死。”著名法学家黄右昌先生在《题赠赵母》中有这样的诗句：“气概黄河三万里，名开青史五千年，闻风可使懦夫立，抗建相期猛着鞭。”<br />
<br />
&nbsp; &nbsp;<br />
国际知名记者和国际评论家宋庆龄的好友金仲华说：“听斯诺先生谈赵老太的故事，北平劫狱西山报信，一个老妇人冲破了敌人重重警戒线，一个中学生正领导游击队，我以为是个神话，见了赵老太南来，游星岛日报，登六层楼台健步如飞，我相信了这个现实，赵老太和赵侗，象征着我们持久抗战中一个游击的新阶段，愿中国的母亲都学赵母，愿中国的男儿都学赵侗。我们祝愿赵母直到抗战的最后胜利。”<br />
<br />
　　1938年9月下旬，赵侗由北平取道天津，乘船南下到香港与赵老太太会面。翌年1月，蒋介石邀请赵老太太和赵侗到战时首府重庆，一时间重庆市万人空巷，欢迎场面空前热烈。著名剧作家的吴祖光以赵侗和战友苗可秀举义抗日为题材的处女作《凤凰城》，由此而轰动大后方。赵氏一家奋勇抗战的纪录片开始拍摄。<br />
<br />
　　1939年3月，赵老太太亦谢绝了国府赠送的汽车洋房，同时也谢绝了一些社会名流奉劝赵侗将军和战友们留在后方休养生息或出国深造。赵氏母子二人与东北抗战的老战友再次招募选拔东北爱国知识青年，组织晋察冀游击纵队。赵侗被国府任总司令，少将军衔，再度率领干部队伍200人分两批由重庆赴黄河再度北上，计划与河北、辽南抗日军旧部会合，巩固扩大抗日根据地。<br />
<br />
　　1939年9月，国府军政两部的部长何应钦和张治中以及妇女指导委员会的指导长宋美龄联合聘请赵老太太为全国义务兵役宣传指导员。先应川北的南充、达县等十几个县的约请，前往宣传抗日，开始了历时两年，奔波百余市县的抗日救国宣传活动。当地群众在赵老太太一家抗战七年的英勇事迹影响下，掀起自愿报名参军抗日的热潮。<br />
<br />
　　1939年12月下旬至1940年1月间，赵侗率部北进途中，于河北新富、灵寿两县交界处的陈庄，突遭偷袭壮烈牺牲，时年仅28岁，国府追授中将军衔。同时牺牲的还有赵老太太的三女儿赵理智，年仅20岁，她曾在抗日战场上死里逃生，被誉为少年巾帼英雄，轰动武汉三镇。<br />
<br />
　　赵老太太在极度悲伤之际，依然奔波四川百余市县，为彻底杜绝强行征兵，抓壮丁等的恶习，向国府争取应征抗日青年的荣誉和待遇，呼吁国府注重优抚抗日军烈属，掀起了自下而上的优抚军烈属爱国运动。又将子女和侥幸生还的战友送往军事院校培训，为重新组织东北和华北抗日旧部继续抗战做准备。<br />
<br />
　　1943年3月，赵老太太与东北同乡国民党中将王达，向国府申请批准挑选部分智勇双全、熟悉游击战术的得力干部，深入湖北武汉一代的敌人心脏，使敌人腹背受敌，牵制业已攻到贵州独山，进而围困四川的日寇主力部队。赵老太太再次将自己的子女送往敌后战场。<br />
<br />
　　1945年8月14日，抗战胜利的前一天，赵老太太的丈夫客死异乡。尔后，赵老太太主动辞去国府军政两部和妇女指导委员会等职务，解甲归田，前往北平。在北平她不顾年迈病弱，四处寻找抗日军烈属，然后上下求助，奔波千里，独自一人冒数九严冬，历时多月，带领抗日军烈属返回东北。回东北后，赵老太太弃武从商，在丹东和沈阳筹办加工厂，试图用自己的力量解决抗日军烈属生活出路。无奈内战骤起，和平建家园希望破灭，赵老太太返北平隐居。<br />
<br />
　　1949年4月，赵老太太由北平再次南下，受蒋介石委托，组织冀热辽边区游击第二路绥靖纵队，四子赵连中任总司令，驻足重庆，以“总统府留守”自任。策划反征粮暴动，提出“赶走共产党，三年不纳粮”等口号，建立大陆游击区，抵抗进川的解放军，阻止国民党十六兵团起义，枪杀解放军谈判代表，围攻占领川西的温江、崇庆、郫县、金堂、新都及川东的秀山等多座县城。<br />
<br />
&nbsp; &nbsp;<br />
1950年2月赵老太太被捕，拒不投降，认为解放军“挑灯拨火，诱惑忠良。”周恩来曾为之说情，免其死罪，但西南军区军法处仍判处死刑，时年已70岁。抗日战争幸存的四子赵连中亦被处决，时年25岁，他的妻子和孩子由毛泽东亲笔批示予以释放。<br />
<br />
　　赵老太太和赵侗将军一家，由组建的少年铁血军开始，毁家纾难，由北退南，屡挫屡战，前仆后继，历经13年的血与火，直至抗战胜利，赵氏家族为国捐躯30余人。</div>

							
							
							
							
