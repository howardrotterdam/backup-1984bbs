																											<div id="postmessage_789723" class="t_msgfont">赵洪文国的反动劣迹<br />
新中国建立之初，土匪特务武装不断制造暴乱，其中爆发规模最大，危害最烈，影响最坏的当数西南地区。我剿匪部队经过长达13年艰苦卓绝的斗争，才完全彻底地将其平息。曾经承担西南剿匪史料丛书和史志课题研究的成都军区政治部编研室的同志，在浩如烟海的史料中，精心选取了有关西南剿匪斗争鲜为人知的故事，奉献给读者，以此纪念中国人民解放军建军80周年。――编者<br />
<br />
　　晚年的邓小平，在回忆进军大西南这段往事时说：进军西南，同胡宗南那一仗打得很容易，同宋希濂也没有打多少仗。真正打了一场硬仗的是剿匪战斗，打得很漂亮。邓小平说话简练，关于进军大西南的事，他只讲了40多个字。可见西南剿匪斗争在他心中的分量。<br />
<br />
&nbsp; &nbsp; 1950年2月，一份邓小平亲自草拟的“关于西南情况和工作方针”的加急电报，放在了毛泽东的案头。邓小平在报告中写道：鉴于西南匪患的猖獗，剿匪已成为全面的中心任务。不剿灭土匪，一切无从着手。当时，不甘心失败的国民党残余势力和封建地霸武装，沆瀣一气，妄图颠覆新兴的人民政权。 <br />
<br />
&nbsp; &nbsp; 在毛主席的批示下，3月22日，邓小平、刘伯承与贺龙联名发布了剿匪布告。邓小平指出，这一任务“要比普通的军事斗争复杂与艰苦得多”，“仍然包括流血和牺牲”，而且“不是打几个冲锋就能解决问题的”。要取得胜利，不仅需要坚定勇敢，更重要的是要有智慧、有策略、有办法。一场大规模的剿匪斗争，就此拉开序幕。<br />
<br />
&nbsp; &nbsp; 蒋介石封的“西南反共救国军第2路绥靖总司令”是个疯狂不可一世的“双枪老太婆”，她落败于解放军的“黑虎掏心术”<br />
<br />
&nbsp; &nbsp; 智擒“双枪老太婆” <br />
<br />
&nbsp; &nbsp; 抗战初期，满洲女贵族赵洪文国将家产变卖一空，带着家人从关外逃到华北，在共产党的帮助下，拉出了一支抗日游击队。从此，赵洪文国去掉金钗旗袍，放开小脚，打上绑腿，腰插双枪，完全一副巾帼女杰的装束。 <br />
<br />
&nbsp; &nbsp; 1940年春，她作为抗日英雄，被国民党接到陪都重庆，受到蒋介石的接见，蒋介石与她合影留念，还亲书“游击之母”四个字赠予她。一时，登门祝贺、目睹其风采或聆听其抗日义举的各界人士，纷至沓来。人们见上了年纪的她仍腰插双枪，便称她为“双枪老太婆”。 <br />
<br />
&nbsp; &nbsp; 抗战胜利后，蒋介石封她为“第2路绥靖总指挥部”中将总指挥。她这时已全然忘记了共产党对其抗日游击队发展、壮大乃至每一次战斗给予的支持和帮助，公然与共产党为敌。<br />
<br />
&nbsp; &nbsp; 1949年秋，赵洪文国所部在解放军的打击下遭到了彻底的失败。她带着家人狼狈逃到了重庆。蒋介石逃离重庆前夕，亲派蒋经国对她封委许愿。在重庆上清寺的公馆里，赵洪文国接过了蒋经国颁给她的“西南反共救国军第2路绥靖总司令”的委任状。自此她带上两个儿子和女婿，连同一些心腹，领了蒋介石亲赠的大批弹药，踏上了川西平原，利用当地袍哥和股匪，兴风作浪。<br />
<br />
&nbsp; &nbsp; 1950年2月9日，四川什邡三河乡，赵洪文国正式粉墨登场。 <br />
<br />
&nbsp; &nbsp; 她公然以“西南反共救国军第2路绥靖总司令”的名义发布公告。一时间究竟封了多少官，她也记不清。粗粗一算就有百十号人被她分封成名目繁多的队长、司令。靠蛊惑人心、发官许愿，她居然纠集了5000多乌合之众。<br />
<br />
&nbsp; &nbsp; 正式扯出黑幡的第二天，她就将三河乡20多名农会干部和工作人员杀害了。尤其令人发指的是，她亲自指挥匪徒活埋我10多名征粮队员。据什邡武工队统计，在她为匪作乱开始的10多天时间里，匪徒们杀害武工队员和群众就达200多人，烧毁房屋300多间，抢走耕牛200余头，各乡老百姓的粮食被洗劫一空。<br />
<br />
<br />
&nbsp; &nbsp; 2月13日，黄昏的川西坝子笼罩在暮霭沉沉中。赵洪文国亲率5000多名匪徒，浩浩荡荡直奔什邡县城。她要实现蒋介石反共复国的迷梦。<br />
<br />
&nbsp; &nbsp; 土匪的队伍中，她乘坐一顶宽大的滑竿，特别显眼。伴着匪徒们的吆喝声，她的上身于滑竿的颠簸中一起一伏，加之手持双枪挥来挥去，活像一个跳大神的巫婆。<br />
<br />
&nbsp; &nbsp; 早已得到情报的解放军537团，抽调一个炮营和步兵营设伏阻截。<br />
<br />
&nbsp; &nbsp; 炮营在山上架好8门迫击炮，对准匪队一阵猛烈开火。她所纠集的乌合之众顿作鸟兽散，她的滑竿也被逃窜的土匪撞倒，她重重地摔在地上，痛得龇牙咧嘴，两个轿夫也没命逃走了。她儿子拼死挤上来，背起她跑回三河乡，她才侥幸逃脱。<br />
<br />
&nbsp; &nbsp; 面对赵洪文国的屡屡进犯，川西军区决定，智取她的指挥部。4月初，解放军537团1营担负了这个任务。<br />
<br />
&nbsp; &nbsp; 绵竹县境内的大石坝一带，是人烟稀少的大坝子。东南部为道路崎岖终年积雪的大山。当年红军二万五千里长征曾路过这里。1营战士沿着当年红军走过的道路，从茂县出发，翻越土门大雪山，不顾朔风严寒，高山缺氧，经过200里的强行军，直抵大石坝。&nbsp; &nbsp; 选择这一奇险路线，一是为了出敌不意，二是由于大石坝一带山区曾是红军根据地，群众基础好。果然，他们抵达后群众纷纷来到部队，要求带路追击土匪。他们说：“匪司令部就驻在红庙场村南头号的一座大庙里。”去红庙场村要通过一个竹索桥。营长李佐军派3连长刘志全带两个排长和几名战士，由老乡带路去实地侦察，结果发现竹索桥没有敌人防守。敌人没想到解放军会“从天而降”。&nbsp; &nbsp; 当夜12时，准时发起攻击。3连两个尖刀班悄然通过竹索桥，直捣匪巢。他们搭人梯翻过一丈多高的院墙，与敌人展开了近战夜战，仅十几分钟就解决了战斗。但却未见“双枪老太婆”赵洪文国。原来她眼见大势已去，装扮成农妇，带着几名亲信，潜藏在附近一群众家中，但很快就被我搜索部队擒获。<br />
<br />
&nbsp; &nbsp; 1950年7月6日，川西军区军法处判处赵洪文国死刑。赵洪文国，终以她人生最后的反动劣迹，被永远钉在了历史的耻辱柱上。</div>

							
							
							
							
