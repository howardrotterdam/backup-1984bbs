													<h2>房地产已成中国心腹大患</h2>
						
						
																											<div id="postmessage_369205" class="t_msgfont">人民日报<br />
<br />
日前，诺贝尔经济学奖获得者泽尔腾先生来中国讲学时表示，中国经济最大的隐忧是房地产市场存在泡沫。无独有偶，一家机构对北京、上海、深圳等地的房地产行业进行调查后得出结论，这些大城市的房屋空置率已经远远超过10%的国际警戒线。<br />
<br />
　　房地产市场存在泡沫，这是一个不容回避的事实。连续7年来，中国的房价大幅上升，远远脱离民众的购买水平。没有只涨不跌的股市，楼市亦然。当越来越多的人投入到炒房大军之中时，房子已经不再是用以居住的场所，而是被有钱人买来卖去的商品。居高不下的房屋空置率就说明了这一事实，在不少地区，这一比例甚至达到了50%以上。<br />
<br />
　　对房地产业的过分依赖，为我国经济埋下隐患。如今，房地产业已经占了GDP的6%，四分之一的投资也在房地产行业，30多个产业与房地产关系紧密，银行乃至大型企业也纷纷为楼市“背书”……房地产过“热”，已经很难用理性衡量。与此同时，民众的收入并没有相应增加，购房已经成为居民的沉重负担。调查显示，一半以上的民众无力买房。<br />
<br />
　　迪拜世界的风险或许是最好的警示。迪拜政府日前宣布负责国有房地产开发企业迪拜世界的重组，并要求债权人接受延期还款。随后国际评级机构纷纷下调迪拜相关银行评级，全球股市出现动荡。有香港媒体认为，迪拜采取的发展模式，与很多新兴市场一样。政府的投资公司大量举债开发房地产，兴建豪华写字楼、酒店及住宅，并向外国投资者开放市场，通过提供种种优惠和给予居留权，吸引外国企业及专才进驻，很快便令经济蓬勃起来。资产价格上升，自然吸引更多游资涌入，把资产泡沫吹得更大。一旦资金链断裂，后果不堪设想。<br />
<br />
　　发展房地产并不是致富捷径，而是一座建筑在浮沙上的城堡。如果实体经济不能有效“止血”或者主动“造血”，经济很难真正“康复”。当世界各国通过增加市场流动性来刺激经济复苏时，如果这些增加的流动性不是流入实体经济而是流入地产、金融及资产市场，那么，说不定第二波通货膨胀和金融危机还会来。<br />
<br />
　　泽尔腾警告说，如果房地产领域出现问题，中国经济增长率可能会下降3%。风险已如悬挂在头顶的达摩克利斯之剑，我们虽然不能预知这把剑何时落下，但宝剑的寒光已经让人望而生畏。好在，日前召开的中央经济工作会议，已经明确将调整产生结构、改善民生现状，作为明年工作的重点。积极采取措施，引导泛滥的资金流入实体经济，实现我国经济的平稳着陆，无疑是明智之举。<br />
<br />
　　但愿房地产泡沫就此打住，不要再继续膨胀下去，否则，泡沫一旦破灭，伤害的不只是寻常百姓，那些炒房者、银行乃至大型企业都可能面临“灭顶之灾”。</div>

							
							
							
							
