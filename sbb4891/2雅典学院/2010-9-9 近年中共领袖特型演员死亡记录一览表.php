2006年10月2日晚，有“大连古月”之称的王立宪，随旅游团在台湾南投县遭遇罕见车祸，当场身亡，时年52岁。这场车祸非常离奇，肇事游览车刹车无漏油，胎纹正常，现场没有刹车痕迹，山壁也没有擦痕，行车记录卡显示肇事时，车速突然高达100公里，游览车直接坠谷。<br />
