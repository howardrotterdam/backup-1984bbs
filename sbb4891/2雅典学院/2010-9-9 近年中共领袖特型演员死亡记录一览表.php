<br />
4.何明志<br />
在30集电视连续剧《奠基者》中扮演毛泽东的何明志，去年7月底，应邀去重庆参加了薄熙来推广的“红歌演唱会”的演出，走下舞台，浑身大汗淋漓，昏迷住院。2009年8月27日凌晨，在四川华西医院病逝，享年51岁。<br />
<br />
古月猝死后第九天，饰演过毛的唐国强在受访时激动地表示，“我不是特型演员！”<br />
<br />
毛的冤魂债主无数，演毛者结局都不妙啊！谁也不想被抓着当替死鬼。<br />
<br />
1997年2月，一群中共元老的特型演员访美，正乘车行驶在距华盛顿不远的平坦的高速公路上，居然在没车冲撞的情况下，司机自己翻了车。饰演周恩来的黄凯、饰演朱德的刘怀正魂断它乡。饰演陈毅的刘锡田、饰演邓小平的卢奇受伤。<br />
