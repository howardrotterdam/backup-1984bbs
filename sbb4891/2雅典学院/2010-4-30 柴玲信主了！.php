																											<div id="postmessage_591193" class="t_msgfont">柴玲的见证<br />
作者：柴玲<br />
<br />
 <br />
 <br />
 我叫柴玲，2010年4月4日，在纪念主耶稣复活的日子里，我在波士顿受洗成为上帝的儿女，主耶稣的门徒。我H爱的丈夫和孩子们以及众多的主内弟兄姐妹一起见证了这一r刻。当牧师奉圣父、圣子、圣灵的名为我施洗r，主的爱、神的灵立刻充满了我，从未有过的耆乐和释放从我内心涌流。我由衷地对主说：主啊，我感谢你救我脱离罪恶，脱离人世和捆绑而成为一个新造的人，我愿望一生一世跟随你、事奉你、荣耀你！<br />
 <br />
<br />
 <br />
<br />
 <br />
<br />
一、孤独厢宓耐年<br />
<br />
 <br />
<br />
１９６６年，在中国史无前例的文革爆发前夕，我出生在山东省沿海的一个小渔村里 ，我的父母是一对年轻的中国人民解放军的军医。尽管文革时整个中国到处是混乱和恐怖，成千上万无辜的人被折磨和摧残，我的家庭在部队里基本上躲过了这场风暴。我对童年的记忆就是孤独。父母亲经常被派到穷苦的农村或地震等其他灾区去抢救那些比我们更不幸的人们，象标语里说的那样：“救死扶伤，治病救人，实行革命的人道主义。”小时候生病难过最需要父母在身边的时候，他们经常派遣在外不能在身边照顾我们，我是在祖母的身边长大的。 <br />
<br />
 <br />
<br />
虽然小孩子的我不全了解父母工作的重要性，但童年的经历使我对他们的献身工作的精神很尊敬，父母的身体力行也使毛主席语录里的教诲在我的幼小心灵里扎根：热爱祖国，热爱人民，随时准备为了一个共产主义的远大理想去牺牲个人的利益甚至包括牺牲个人的生命。那个时候跟很多同龄人一样，我从来没有想到要怀疑伟大领袖对远大理想的定义和说法，也没有真正体会到牺牲的全部含义。<br />
<br />
 <br />
<br />
虽然现在了解到，想做好事的原因和动机是上帝与我们同在的表现，但那个时候我们并不认识上帝。那个时候我们是不允许认识上帝的。记得小时候提及“上帝”的书都被列为禁书，按列宁的话：“宗教是统治者用来麻醉人民的鸦片”，是资本主义麻醉人民的邪恶工具，“上帝”是一个不可公开传颂的字眼。结果是，一个没有上帝的社会里，很少有爱。在我长大走出的军队营房时，我才了解到这个社会上到处都有仇恨，怀疑和恐怖，没有想到成人的社会里比幼时更孤独。<br />
<br />
 　<br />
<br />
 <br />
<br />
毛主席去世后，文化大革命终于结束了，新领导人鼓励教育和改革。在短暂的一段时期内，我们国家充满了希望和活力。那个时候，我们家对教育的重视以及孤独的童年使我成为了一个好学生。昼夜不息的学习和努力使我成了个典型的永远没有安全感总在不断努力的好学生。17岁的时候，我跳级进入中国最好的大学北京大学学习，成为中国极少数的全国三好学生中的一员，实现了父母和祖先“赶考进京，科学救国”的梦想。<br />
<br />
 <br />
<br />
 <br />
<br />
 <br />
<br />
二、在北大初遇耶稣 <br />
<br />
 <br />
<br />
一天傍晚，他进到一个很偏远的村子里。这个村子又穷又偏僻，女孩子长大都嫁到外地去，几十年来还没有一个媳妇娶进来。村里穷得叮当响，连鸟从天上过也不下来歇歇脚。突然有这么一个大学生骑着自行车闯进来，全村人都惊喜的不得了。村里的长老把大家召集到一个用土坯和麦梗搭起的草棚里，这个研究生和长老被围在中心，农民很敬畏的把一个用黑色油毡的包裹被送到他的身边。老村长小心地一层一层地打开，生怕弄坏了，原来里面是一本很旧的圣经，已经发黄和破碎，是四十年前解放初期，外国传教士被迫离开中国时留下的。这些年来，历经那些运动，村民们一直冒着生命危险保存着这本圣经。遗憾的是，村里能识字的人或者过世了，或者离开了，这些年来，村民们没有一个人能看书写字，每次礼拜时他们都是用手摸一下包在油毡里的圣经，用手的触摸与上帝的精神进行沟通。现在终于这么一个有知识有学问的大学生从天上掉下来，村民们的祈祷终于被听到了。感觉到上帝听到他们的呼声更加鼓舞了这些贫苦和绝望的人们，他们更殷切地想知道圣经里面都说些什么。那天晚上，这个研究生借着微弱的油灯，读呀，读呀，那些村民们站着望着他，如饥似渴地听着从他嘴里读出的每一个词；他们一动不动地听着，直到窗外破晓。村民们得回田里干活了，他们不情愿地离开了。<br />
<br />
 <br />
<br />
为了感谢我的朋友向他们传达上帝的话语，村民们给了他一大袋红薯带回家――这是他们唯一能在村子里找到的礼物。我朋友的旅行漫长而艰难，他必须扔掉许多东西，但是那袋红薯他一直保留到最后。<br />
<br />
 <br />
<br />
我那时还是一个在校园里干劲十足试图同时干很多事的年经女大学生，但这个故事让我停了下来。我被他所看到的感动了，吸引那些村民的精神也强烈地吸引了我。因为我们从小到大一直被要求要热爱共产党领导人和毛主席，我看到人们做的一切都是出于害怕或无奈，从来没有看到人们公开表现出的这样一种真诚强烈的忠诚和信仰。同时由于另外一些我时至今日都不能理解的原因，我的生活已经改变并且慢慢失去控制。<br />
<br />
 <br />
<br />
 <br />
<br />
 <br />
<br />
三、1989天安门民主运动<br />
<br />
&nbsp;&nbsp;<br />
<br />
我最终参加了1989年天安门运动，希望有一个更民主，更自由，更美好的中国。在1989年春天的那50天里，我看到了希望和爱带给人的力量，在一个炎热的夏日，一个贫穷羞涩的男人硬塞进我手里五元钱让我买些水给我的学生们喝，在那一瞬间， 我理解了这是他爱的表达。当我把他的爱变成买来的冰棍水的时候， 饥渴疲劳游行的同学们也唤起了爱和温暖。同学们的爱和温暖又传进了更多北京人的心里。 这份爱和温暖融化了很多年来的仇恨和不理解， 唤起了更多人的爱和温暖，于是， 整座北京，整个中国，整个世界都被爱和希望的力量和希望所笼罩，在哪短短的50 天里， 我们冲破了魔鬼用仇恨，暴力，孤立和恐惧制造的枷锁， 真正体会到做自由人的尊严和美好。那个时刻，我们也找到了真相：我们爱人民，人们也同样会爱我们。我们爱祖国，那么祖国也会爱我们。甚至北京的小偷们也发起了罢工，以停止盗窃来支持我们的运动。天安门事件在世界舆论的帮助下不断地扩展，最终成为全球性事件。这次事件触动了其他文化环境和很多人。这次事件以难以想象的程度改变了我们每个人的生活。<br />
<br />
 <br />
<br />
那么执政者方面的真相是怎么样呢？他们能够理解我们的爱心吗？他们会允许人民自由吗？现在我们知道了他们没有。六月三号晚上， 最后的时刻来临了，当坦克和军队蜂拥而来的时候，我与最后的5000名学生从广场撤离。我看到了屠杀后的长安街，空气都迷谩著恐怖和愤怒。 在火与血的惨烈中， 我们看到了执政者给我们的回答：当我们付出爱和和平的时候，他们却回报给我们死亡和屠杀。起初我们没有人能够相信。我怎么能够相信人民解放军会杀死自己的人民？我就是军人的后代。对我来说，他们是叔叔和阿姨，是兄弟和姐妹。我们爱他们，他们怎么会不爱我们，要惨杀我们？<br />
<br />
 <br />
<br />
那个晚上， 在我们不得不被迫放弃我们指挥部的帐篷时，我抬头望向天空，痛苦地询问：“为什么？为什么？为什么？我们只是想进行对话，而他们为什么却一定要杀死我们？”<br />
<br />
 <br />
<br />
尽管我们哭泣，我们不能相信，但死亡还是一步步的朝我们走来了。在那50天里，我不得不克服许多新的情感和考验，从生活简单的研究生到群众运动的领导者；从一个无忧无虑热爱跳舞的年轻人到处于现在可能失去自由和生命；从梦想到美国学习然后开心快乐地生活到可能面对永远失踪的可能， 而现在，我们面对的却是毫无疑问的死亡的逼近。<br />
<br />
 <br />
<br />
从小孤独地在军队医院周围长大的我总是很害怕死亡。有一次看到死人从太平间里抬出去，我便停下玩耍，跑回家里躲避，当晚连做噩梦，梦到自己掉进一个深不见底的黑洞里，无穷无尽地下坠， 我的心都被悬着窒息， 每次都在尖叫中醒来。但是在一九八九年六月三日的晚上，这不再仅仅是梦了，这次是真真切切的，我不知道怎么去克服对死亡的恐惧，也不知道怎样去征服对父母兄妹和爱人的眷恋。许多想法一起涌上心头：千里之外，如果我不在了，我的家人会变成什么样, 他们该怎么活下去哪？生病的母亲会怎样坚持下去，疲劳心碎的父亲会怎样地生活下去。我的夫婿好年轻，我们曾梦想的家庭和儿女都没有时间实现 …. 如此多的问题，如此短的时间。没有答案。那么，最后的时刻就这个样子了！就这么完了？<br />
<br />
 <br />
<br />
 <br />
<br />
 <br />
<br />
作为一名学生，我可以只考虑自己的想法。但是作为一个学生领袖，我不能只沉浸在个人的苦恼中， 我必须去看望一下我的同学们。在纪念碑的西北面， 在灯光下，我看到他们的表情跟我的感受一样：悲哀，难过，极大的无助，低落， 极大的困惑和对死亡无名的恐惧，像是在等待宰割的羊羔一样。那时，所有的豪情壮语都消失了，死亡的威胁象大浪淘沙一般，洗净了所以尘世的浮杂和嘈闹，只剩下了最纯净最真实的感觉: 那是一种多么绝望无奈无助的感觉，就像我运动中一直都不愿面对承认的感觉：有心报国，无力回天 --- 哀！<br />
<br />
 <br />
<br />
古人说“人将去矣，其言也善， 鸟将去矣， 其鸣也哀。”今天，当我们在青春年华，面对死亡最后的一刻时，我们都在心里默默地念着绝食书里的话，对我们生命里最热爱的人们告别， 对我们对生命里最珍贵的事物告别，<br />
<br />
 <br />
<br />
“别了，同仁，保重！死者和生者一样地忠诚。<br />
<br />
 <br />
<br />
别了，爱人，保重！舍不下你，也不得不告终。<br />
<br />
 <br />
<br />
别了，父母！请原谅，孩子不能忠孝两全。<br />
<br />
 <br />
<br />
别了，人民！请允许我们以这样不的已的方式报忠。“ <br />
<br />
 <br />
<br />
 这些写在5月13号的话， 在6月3号的晚上不幸的印证了。我默默地难过的地看着我亲爱的同学们，在这样生命的最后一刻，在我们不得不做出为了一个更高的理念去舍弃一切人世珍爱的时候，我还能有什么样的语言来安慰他们， 我能有什么样的话语再给他们勇气和决心？在那个最后的时候，许久以前的一个蚂蚁的故事突然涌上心头，我温柔地把她分享给同学们：<br />
<br />
 <br />
<br />
“从前有个山， 山上有很多蚂蚁。一天山上着火了，蚂蚁们必须转移到山脚下才能存活。他们聚成球状，穿过火海，结果外面的蚂蚁被烧死了，但里面的蚂蚁却存活下来了。今天晚上，我们就是站在祖国最前线的蚂蚁。因为我们的死亡，会换来一个自由民族的诞生…”　欢笑鼓舞重新回到我们学生们的脸上，在告别生命的最后时刻，我们共同找到了自己的牺牲的原因和目的。这个共同的原因和目的给了我们共同地带来了去面对死亡和黑暗的勇气。就在那个时刻，在同学们的鼓舞和掌声中，一股巨大的爱的暖流席涌到我的全身，我的眼睛离开了可爱的同学们，移向广场背面那无穷无尽的士兵和坦克的黑影里，移向天安门背后深远黑暗无边的中南海里，移向那与地面分不开的黑暗的天空里，我多么由衷地感到在我们准备把最宝贵的生命奉献给人民和世界的时候，我们对人民和祖国的爱是多么的强烈啊。我多么希望有一天，那些从没认识过我们的陌生的中国人在享受到我们的鲜血和生命所换来的幸福和自由的时候，他们能够记住我们是曾经多么地热爱着他们；我多希望在中南海里下令开枪的领导人能够感受到我们无限的爱，我多希望在广场长安街上手持屠刀的士兵们能感觉到我们无限的爱！… 感谢主，奇迹般地，我活了下来。６月４日清晨，在多数同学决定撤离时，我们指挥部带领着最后5000多人走出了广场。今天回顾，我相信我奇迹般的生还一定是上帝的作为，但是我永远我会忘记在生命最后一刻我们对人类的爱的体验，　这一定是那些没有能够生还的同胞姐妹们最后的感觉。也是耶稣基督在十字架上的牺牲精神的延伸。<br />
<br />
 <br />
<br />
在大搜捕的日子里，我每天面对著被捕的危险，我也得到了许多有良知的人的帮助。我在中国躲过了十个多月的搜捕，最后，是一群佛教徒冒生命危险救了我。我独自一个人在一只船里的木箱内忍受了4天5夜的完全的黑暗，而这旅程本来只需8小时，&nbsp;&nbsp;<br />
<br />
 <br />
<br />
终于在1990年复活节的前夕月我成功逃抵香港，再到巴黎，最后抵达美国。<br />
<br />
 <br />
<br />
 <br />
<br />
 <br />
<br />
四、生命的破碎 <br />
<br />
 <br />
<br />
移民的生活并不容易；而像我这样不太会说英语，心中仍爱一个她不能够回去的国家的难民来说，生活就更加困难。<br />
<br />
 <br />
<br />
来美国不久，我的婚姻破碎，我们这对北大校友；经过“血与火”走出来的“患难”夫妻在获得自由后却分手了。儿r的恐惧孤独再一次包围著我。更致命的是我的母H和祖母相继去世了，我的母H25岁生了我，而当我25岁r她离开了我……家破人亡，我生命中最爱的三个人在短短的几个月内一个个离开了我，而我喜欢的那份工作不愿意因为我而破坏中国区的生意，接着是一些恶意中伤和攻击，每一次黑暗时刻的来临都比前一次更猛烈。这就是我为民主付出的代价。这一切对于我太沉重了，打击象海浪般排山倒海涌来，使我窒息。<br />
<br />
 <br />
<br />
我害怕黑夜的来临，漫漫长夜中我常不知所措，而神]有忘记我，当地华人教会的弟兄姐妹常为我祷告，安慰我，帮助我，我的第一部汽车也是当地华人教会的信徒送给我的。尽管我当r还无法接受耶稣作生命的主，但却透过教会感到主的爱和接纳。<br />
<br />
 <br />
<br />
感谢主，这十几年来主耶稣没有放弃我，丢下我，主帮助我拿到普林斯顿大学的国际关系硕士学位和哈佛大学商学硕士学位，使我能胜任政府和非政府组织以及美国公司的工作，神也奇妙地把一个爱主的美国弟兄带到我的身边，在十年间，我们彼此相爱，我们有了三个可爱的孩子；我们也把家庭扩大到了中国，捐款帮助那里的孤儿；除了家庭我也创了一个软件公司，感谢主，我们现在已有280个雇员，公司正在稳步健康地发展。这些都是神的恩典！<br />
<br />
&nbsp;&nbsp;<br />
<br />
去年春天，正值天安门事件20周年纪念，当年天安门事件中的同伴方正来找我，这让我吃了一惊。在广场上，我并不认识方正。我后来了解到在６月4日我们离开天安门广场的时候，我在前面引导队伍前进，而他是走在最后面的。后面的坦克高速驶来，方正经历了广场上最后一刻那最强烈的爱的洗礼，下意识地在生命最宝贵的几秒钟里，自愿的把旁边的刚认识的年轻女孩推到安全处，而他自己却失去了可以拯救自己的宝贵几秒种，他的双腿被坦克压扁，另外几位同学们却活活地被压成肉饼。<br />
<br />
 <br />
<br />
因此20年后，我是见到了坐在轮椅里的他。听着天安门事件后他的故事，我想当时如果坦克不是从后面而是从前面驶来，我的生活也可能像他那样。他说随着时间的流逝，越来越多的人从中国的经济发展中受益，也便淡忘了当年的牺牲。天安门事件的受害者被遗忘了，他们每天都在贫穿和病病中挣扎。<br />
<br />
 <br />
<br />
方正的到来对我的心灵上带来了很大的震撼。但我在海外民运中备受伤害，不得不把我的心灵紧紧地对世界关闭起来，这十二年里，我一直有一个信念：只要有一天，我把公司做大，我就可以建立一个大基金会，那个时候我就会有能力来继续帮助那些受迫害的人。但是方正的到来把我从自己的隔离中惊醒，使我看到了天安门事件受害者的挣扎和苦难。我不能在等下去，我必须马上立即打开资源，帮助他们走出疾苦，使他们在这样漫长的黑暗里不要丧失希望。<br />
<br />
 <br />
<br />
于是在六四二十周年的日子里，我承诺未来五年捐一大笔款以帮助他们。<br />
<br />
 <br />
<br />
因为我这个承诺，接下来的夏天成为我生命当中的另一个黑暗期。这次，中国政府代理要求我的家庭成员直接传达给我一个消息，那就是我捐款不会给我以及我的家庭带来任何好处。尽管生活在美国这片自由的土地上，我仍然感受到乌云压迫在我头上。这片乌云时刻笼罩着我。在表面上看，我过着完美的美国式生活：可爱的孩子和丈夫，家庭，公司，私立学校，假期，但是我感觉到自己被监视和跟踪，我的电话被监听，我们的电脑被侵入，以及导致一些持不同政见者神秘的车祸事件。那么他们有没有做过其他事情？他们下一步要做什么？在这片自由的土地和勇敢者的家园上，我并不觉得安全或自由。<br />
<br />
 <br />
<br />
 <br />
<br />
为了天安门运动痛失了许多亲人之后，我对新家庭的爱已超过了一切。我对年少无知的孩子以及亲爱的丈夫负有责任。我的丈夫是典型的乐观善良有正义感的美国人，对于另一个黑暗政体的残酷和血腥并不知晓，他最爱的消遣方式是看红袜队，爱国者队以及其他一些他可以抽空看的体育比赛。但是，我是一个北京屠杀的辛存者，我有著良心和道义上的责任。我陷入了两难…… <br />
<br />
 <br />
<br />
方正被安上假肢后，他与他太太第一次跳舞，很多人为此激动落泪。为了庆祝他重新站立起来，我要坐飞机去华盛顿。在飞机上，撒旦开始攻击我……半昏迷状态的我醒来感到窒息和眩晕，一种将死和失去控制的感觉。这种黑暗势力的攻击，是我在天安门广场也未曾体验过的。但是这次，我陷在了过去和将来之间，我失去了所有的目标和意识，我丢掉了所有力量。我呼求主耶稣的帮助，攻击才慢慢退去。<br />
<br />
&nbsp;&nbsp;<br />
<br />
这个经历使我终于看清了自己的本相，我意识到，面对一个强权国家自己是多么的渺小。我，一个卑微的个人，如何同拥有大量资源和社会网络的国家相抗衡？我怎么可以参加一个注定要输的战斗，而在战斗的过程中，以牺牲我最爱的人为代价？<br />
<br />
 <br />
<br />
 <br />
<br />
 <br />
<br />
五、直面耶稣<br />
<br />
 <br />
<br />
在过去的岁月中，许多人已经在传播关于耶稣的信仰。在南卡莱罗纳州开办神学院的好朋友杰瑞・亨特（Jariy Hunter）以及卫理公会教派牧师科德角（Cape Cod）都向我介绍耶稣。近来普林斯顿的余英时余师母也鼓励我看一看圣经。当然还有那些从学生领袖成为传道人的朋友们。<br />
<br />
 <br />
<br />
一天晚上，我遇到一位叫小约翰雷吉（Reggie Little John）的新朋友。她是虔诚的基督徒，并且相信自己领受了上帝的使命来关注中国强制堕胎的野蛮事实。她说她直到自己熬过一场危及生命的疾病之后才真正认识上帝，她还告诉我自那场病后从她从一个诉讼律师改行做人权人士和电影制片人，而现在她把自己的时间用来为服事上帝。<br />
<br />
 <br />
<br />
我问她有没有看见过上帝？她说她自己没有，“圣经说：没有人见过上帝，惟有上帝怀里的独生子将他表明出来。”信耶稣就是信上帝。她送给我一本书，叫《天上的人》。这本书讲述了一个中国年轻农民因为神奇迹般地救活了他病危的父亲而归向耶稣的故事。后来他成了一个河南村传道人。并且因为传福音而多次被送入监狱。一次，他被狱警打断了腿，但是在主耶稣的帮助下，他在众目睽睽之下走出了监狱，而他并没有意识到自己的腿已经被主治愈了。在这本书的最后，他问道，“那么你准备好跟随耶稣吗？”<br />
<br />
 <br />
<br />
我合上那本书，拿起电话，“雷吉，如果你认为每个读完此书的人都会成为基督徒，那么你肯定是疯了。”我只是在半开玩笑。对我来说，信仰耶稣就等于是回到那些迫害。我花了20年的时间才刚刚让自己从那里摆脱出来。<br />
<br />
 <br />
<br />
同时，雷吉给我了一部关于约翰福音的电影，每次看着电影，听着耶稣的话，我都感觉到自己燃烧的心中有一种难以解释的平静和安宁。几个星期之后，事情一环扣着一环，戏剧化的事件也出现了。雷吉和我参加了美国国会人权委员会对于中国强制堕胎的做法的审判。我觉得，直到听了吴娟的故事，我才真正了解中国。<br />
<br />
 <br />
<br />
吴娟在拿到准生证以前就怀孕了，她要隐藏她怀孕的事实希望能保住她的孩子。计划生育部门的官员们殴打折磨她的爸爸，强迫她在父亲和孩子两条生命之间做出选择。她父亲没有泄露一点消息，但是计划生育部门的官员们还是想法找到了她藏身的地方。她被拖到一个医院强制堕胎。两针打下去，孩子不动了，但是却奇迹般地留在了她体内。那个时刻，年轻无助的妈妈吴娟对她的孩子感到很骄傲，并且鼓励孩子说：“宝宝真强壮，也许宝宝能逃过堕胎针活下来。”但是第三天，吴娟还没明白怎么一回事，她就被拖进了手术室，剪刀插进了她的身体，把她的孩子剪成了碎片。在她无助的眼泪和哭喊声中，她能听到的只有剪刀的咔嚓声。最后，医生拽出一只婴孩的脚，五个饱满的小脚趾就像玉米粒似的。这个场面深深地烙在吴娟的心里。孩子死了，吴娟生命的一部分也离开了。她讲到这里，这个调查庭只有我们和吴娟的流泪和哭泣声。在痛苦和悲伤中，我们的心碎了，因为吴娟和她无助的孩子，为了中国每个妈妈和她们未能出生的孩子。这个时刻让我回想起1989年6月4日那晚的屠杀中我们经历的所有无助和痛苦。那是个那么野蛮的夜晚，但我们没有力量去阻止，世界的其他人也不能。<br />
<br />
 <br />
<br />
 <br />
<br />
 <br />
<br />
吴娟的故事只是2005年发生在中国仅一个县里10000件案例中的一件。在过去的三十年中，中国大约有4亿的生命因为堕胎被野蛮地剥夺；很多是以这种残酷非人道的手术方式进行的，不但结束了婴儿的生命，也使绝望的母亲的心灵遭受了严重的创伤。在强制堕胎之后，吴娟一直挣扎在生死边缘。她是在信仰基督后才活下来的。其他人就没有这么幸运了。在中国，每天有500名妇女自杀。很少有新闻报道过她们过早的死亡。<br />
<br />
 <br />
<br />
 <br />
<br />
 <br />
<br />
在极度的惭愧中，我意识到了正在发生的事情。尽管已经过去20年了，没有人能够忘记1989年的天安门大屠杀。但是我们中没有多少人能意识到计划生育政策要比光天化日下发生的天安门屠杀残忍上千倍，百倍，而且每一天这种残忍都在重复。吴娟哭着说，“谁能帮助她们？谁能拯救她们？ 在中国，计划生育政策和强制堕胎政策已经杀死了几百万的无辜生命。怎样才能制止这种非人道的犯罪？何时才能制止这种非人道的犯罪？”<br />
<br />
 <br />
<br />
 <br />
<br />
 <br />
<br />
六、耶稣慈声的呼唤<br />
<br />
 <br />
<br />
 <br />
<br />
 <br />
<br />
吴娟的哭声使我受到了极大的震动。如果有谁可以阻止这种野蛮，那必定是上帝，也只有上帝。因为我曾经努力过，上帝知道我曾经是多么虔诚地努力过，但我们没有成功。再一次，我发现自己在上帝的国门前徘徊，想去相信却有许多问题。首先的问题是：“佛教怎么办呢？我的基督教信仰是否就是对那些曾经救我生命的佛教徒的背叛？“<br />
<br />
 <br />
<br />
我亲爱的丈夫回答说，“不是的，神爱每一个人。”<br />
<br />
 <br />
<br />
“如果我请上帝来拯救我，成为我生命中的主人，我的生活会失去控制吗？我会成为归回耶路撒冷运动中的一员吗？对于中东我一无所知，而且我也不知道我的孩子们会不会喜欢他们的食物。”<br />
<br />
 <br />
<br />
“上帝会让我们做他已经为我们安排好的事情，一项他专门为我们每个人设计好的任务。”雷吉在我到达终点前的几步里帮了我。<br />
<br />
 <br />
<br />
“那么上帝给我的计划是什么呢？”我问，这个问题在过去20年一直纠缠我，到现在我已经是一片混乱。<br />
<br />
 <br />
<br />
“我相信主肯定会用自已的方式使用你。因为你在中国长大，受过极好的教育，也受过许多的苦难，嫁了一个美国丈夫，创办了自己的事业，这么多特别的经历使你更蒙主使用…”<br />
<br />
 <br />
<br />
她说话的时候，我心里渐渐变得清晰，许久来我一直不愿打开的记忆之窗开始在脑海里呈现出来，１９８９年中国北京的５０天里充满的希望和爱，以及在６月４日我所刻骨铭心的爱的记忆，我脱口而出：“我多么希望能够把上帝主耶稣的爱带到中国，希望人们能够感觉到上帝的爱心，治愈心灵上的创伤，那么这么多的悲剧就不会永远发生，那时的中国会成为多么美好的地方啊…”<br />
<br />
 <br />
<br />
“这就是主对你的生命的使命，把它写下来。”雷吉打断我，“把主耶稣的爱带到中国――这就是上帝给你的计划。”<br />
<br />
 <br />
<br />
“哇，这是项宏伟的工作。我该从哪着手呢？”因为我记起了当年对天安门事件的信仰以及随之而来的痛苦和牺牲，“我还要再一次的经受失去所爱之人的悲痛吗？”<br />
<br />
 <br />
<br />
“上帝会安排给我们可以胜任的工作。像科里泰纳布（Corey Tenneboom），她为了保护犹太人而失去了很多她爱的人，然后当她意识到自己不能继续做这种拯救工作了，上帝便安排给她一项同等重要的工作。”<br />
<br />
 <br />
<br />
“那么我的孩子们会怎样？”我几乎感觉到主正通过雷吉同我交谈。“耶稣基督是非常仁慈的，他会温柔地指引那些有孩子的人。比如，耶稣基督直到他年幼的弟妹们长大成人才开始传教。”<br />
<br />
 <br />
<br />
“那么那些迫害呢？我还要重新经历那些痛苦吗？”<br />
<br />
 <br />
<br />
这时候，雷吉的回答变的柔软而温和，“柴玲，鉴于你曾经的经历，你所忍受的痛苦，上帝会知道你是很认真地信仰他的，以及你准备好为他再做奉献和牺牲，上帝会特别的珍惜你对他的信仰和皈依…”<br />
<br />
 <br />
<br />
 <br />
<br />
 <br />
<br />
七、邀请基督进入我的心灵<br />
<br />
 <br />
<br />
 <br />
<br />
 <br />
<br />
那是个周五下午，上帝通过雷吉的话深深地打动了我。这么多年来，我为中国的民主事业做出这么大的努力和牺牲，不但没有得到理解，反而遭受世人无情的攻击和诽谤。我从此不再相信世间有公正，也不相信人间有友情。杀人者可以逍遥法外，被杀幸存者却要被言论封杀唾弃。但是在人间的所有不公正之中，万事清楚，万事万能的主耶稣基督却完全理解我过去的纯真和献身，因而会格外珍爱我对他的皈依。基督的话使我封闭了２０年的厚重心灵之门第一次被打开了，深藏在里面的那个遍体鳞伤的心终于被上帝的理解和公正被感动了。憋了２０年的终于被理解的感激泪水象开闸的龙头，哗哗地从脸上涌出。生怕雷吉听到我的哭声，我急急放下电话。<br />
<br />
 <br />
<br />
在那个时刻，我再也没有任何等待迟疑，急急走到办公室的角落，跪下，衷心地祈祷，“亲爱的天父，H爱的主耶稣基督，我感谢你十字架上为我们的牺牲，你是我的主我的救主，我现在请你进入我心中做我的救赎主和我唯一的神。请你赦免我所有的罪过，无论是已知的还是未知的，请到我的心里，给我新生命，引导我的生活。我会由衷地追随您，我的救主，耶稣基督”……<br />
<br />
 <br />
<br />
这是12月4号星期五，我感到很不好意思，晚饭后过了几个小时才告诉丈夫下午发生的事情。他很开心，为了我，也为了我们家。他是个虔诚的基督徒，从十二年前我们开始约会起，便时不时地带我去教堂，他的爱耐心而完整。现在他很喜乐。我们度过了一个美好的周末，看了《万王之王》和其他更多的一些电影。这是长期以来第一个我感到完全平静的周末。最终上帝成了我的主人，在他无边无尽的佑护下，我从六四以后一直下意识的颠沛流离，时刻准备迎接攻击伤害的心放下来，第一次真正地进入了甜美安详的梦乡！<br />
<br />
 <br />
<br />
 <br />
<br />
 <br />
<br />
八、与战友们在基督里重聚<br />
<br />
 <br />
<br />
 <br />
<br />
 <br />
<br />
直到下个周一，我才把这个消息简要地告诉了另外一个天安门事件的朋友周封锁，在20周年纪念的时候，他曾经和我在华盛顿一个古老的L老会教堂里参加了一个很长的祷告会，并且把我的灵魂奉献给上帝等待他的教导和呼召。5个月之后，我成了一名基督徒，他很开心。他当时与我们都在天安门，像我一样也是在21名通缉犯中。不幸地是，他被抓到，在监狱里呆了几年。十几年前，他来到美国时同样很憔悴。他的心里满是复仇和挫败感。就在那时，他皈依了基督，在上帝的面前放下包袱，重新找回了平静和力量。是他努力把方正带出了中国，方正的出来导致我的决心，由此开始的一连串事件最终引我走进主基督的怀抱。<br />
<br />
 <br />
<br />
付希秋（Bob Fu），一位援助中国家庭教会运动的朋友听到了这个消息后，给我发来了封热情洋溢的短讯。他和家人的牺牲和奉献也是世人要记住感激的事情。<br />
<br />
 <br />
<br />
还有当时天安门运动中的副总指挥张伯笠，他曾在中国躲避两年的追捕，过著地下生活，他在逃亡的旅途中找到了耶稣，现在在美国做牧师的他给了我许多的帮助，每次通电话都用主的话鼓励我坚固我，并为我祈祷。<br />
<br />
 <br />
<br />
另一位朋友熊焱也是天安门事件中的学生领袖，在监狱里过了几年，现在他作为牧师在美国军队里扮演卓别林的角色帮助战场上的士兵，从伊拉克回来后也给我发来了愉快的短信。上帝对他的奉献是如此地爱护，一次在伊拉克时遇袭r，对方的炸弹打到他的帐篷边都不能爆炸。<br />
<br />
 <br />
<br />
我发现，平时共事为中国的妇权和儿童权益奔波的的张菁也是一位虔诚的天主教徒。<br />
<br />
 <br />
<br />
短短的几天内，我长久失去联系的朋友们因为我信仰基督教而与我重聚。这次，我们的连结和信任因为共同的信仰而加强了。<br />
<br />
 <br />
<br />
最后是引导我冲破终点线的亲爱的姐妹雷吉。当我告诉她的时候，她开心地尖叫了起来。平静下来之后，她给了我更多书和CD，让我继续了解耶稣。在她西部的家里，她建议我去公园街教堂找一位导师指导我学习圣经。<br />
<br />
 <br />
<br />
这就是为什么我们今天会在这里；我们遇见了波士顿历史悠久的公园街教堂优秀的主任牧师戈登（Gordon），我们的新朋友钟约翰（the Rev.John Chung），以及我祈祷希望找到的圣经导师，就是站在我旁边的可爱的塔米（Tammy）。<br />
<br />
 <br />
<br />
尽管到现在为止只过了五个月，但是我无法全部列举出上帝让我们做出的以及通过我们所做的惊人的改变。<br />
<br />
 <br />
<br />
首先，主帮助我们在中国找到了被贩卖的孩子，实现了奇迹般的重聚。这个小女孩在7岁的时候失踪，25年后与她的家人重聚。 <br />
<br />
 <br />
<br />
主耶稣通过父亲无意的验血过程让父亲防止了突然心脏梗塞死亡的可能。我们得知父亲患上了白血病。通过圣诞前夜的祷告，神的医治和安慰临到我们，我们了解到上帝已经确保父亲所得的白血病是危害最小的那种，在药物的帮助下有痊愈的机会。<br />
<br />
 <br />
<br />
主也开始改善我长期以来忽视的健康，我甚至减掉了顽固的婴儿肥。而且神也把我的孩子们召唤到他身边，孩子们是和我一起受洗的，现在一想到主耶稣会守护着他们，把最好的东西带到他们生命里，我就觉得很快乐！这也是父母能够给孩子们的最好的礼物。<br />
<br />
 <br />
<br />
感谢神，赞美主！我们的家庭成从没有象这样与主耶稣更近而像现在这样更亲密，快乐，热爱过，我们的婚姻在主里更加美满幸福。<br />
<br />
 <br />
<br />
有趣的是，我们的事业也在改变。在我们事业前进的时候，我们更有目标感和更强烈的使命感了。我的二十年都不能完成的天安门回忆录在一神奇的速度写成，我相信只有上帝才能使这项工作这么快的完成。而且我现在融入了有信仰的人组成的新社区，比如其中的塔米（Tammy）就是一个很棒的神学家兼导师。她很大方地抽时间在周末教我学习圣经，教我完成新基督徒要做四个基本步骤。在我的一生中，我一直在一个人挣扎和抵抗，我从来没有现在这样感觉受到彻底的欢迎，教导和接纳。<br />
<br />
 <br />
<br />
圣经说：“所以你们不再做外人和客旅，乃是与圣徒同国，是神家里的人了。”我终于回到家了。回家真好！<br />
<br />
 <br />
<br />
 <br />
<br />
 <br />
<br />
九、上帝和中国<br />
<br />
 <br />
<br />
 <br />
<br />
 <br />
<br />
当我在圣灵引导下回顾中国的时候，我的整个人生终于有了意义。中国家庭教会的发展对我的启蒙终于导致我走上天安门，天安门屠杀暴露了共产党政权的残暴真相。在过去的20年中，中国同胞完全失掉了对共产主义的信仰饥渴寻找时，上帝靠中国家庭教会的蓬勃发展把主的福音和救赎传遍中国大地，使一个新的中国在和平的方式中诞生，当今，中国无数信仰饥渴的人来拥抱耶稣。圣灵的风将耶稣的大爱和他的福音带到了地球上人口最多的国家。就像当年耶和华从埃及那里将以色列拯救出来一样，上帝现在也正将中国从共产主义的禁锢和苦难中解救出来。<br />
<br />
 <br />
<br />
上帝是如此地仁爱善良，尽管中国曾经对主耶稣和他的信徒做过很多邪恶的事情，比如在19世纪初期杀害传教士和中国基督徒，但上帝都从来没有放弃过中国。虽然在成长的过程中，我们并不知道上帝，但是上帝在我们生活中无处不在：在我不知道如何做完美的祷告时，上帝回应了我的祈祷，使我成为一个优秀的学生来逃离那个小渔村；把六四的同学带回北大，不知道如何逃离的时候，我们得到一本圣经，但是当时愚蠢的我们却说“圣经难道能帮助我们藏身吗？”　即使这样，上帝也没有动怒于我们，他启动一些虔诚的佛教徒去冒着生命危险来营救我们。在任何艰难和考验中，上帝并没有阻止坏事的发生，但确保在艰难的旅程中总有不同的天使护行。我们当初开始民主运动的时候，高喊着人人生来平等，但心里总是不敢肯定。现在我知道我可以自信地说，因为是上帝按着自己的样子创造了我们，所以在神的面前人人平等。<br />
<br />
 <br />
<br />
上帝之爱是如此的广阔。现在，我真切地理解了耶稣被钉在十字架上时为我们付出的爱，就像当年我们在最后的时刻 ---- 1989年6月3日晚上我们准备好奉献出自己最宝贵的生命和爱给中国，不同的是那r我们是为了我们所爱的国家和民族，而耶稣在十字架上奉献出了自己，他为的是全人类的罪人能得自由和永生！因为主耶稣基督的牺牲和受苦，我们才能与基督联合，与天父和好，有圣灵同在，获得永生。皈依主后，主会在我们里面，我们在主里面，我们不仅未来在天堂中获得永恒的生命，而不是孤独地面对永远的黑暗的同r，在地上存活的r面里也有主每天引导前行！<br />
<br />
 <br />
<br />
上帝的宽恕是如此彻底，即使那个同他一起被钉死的两个罪犯之一，当他对他的罪行忏悔的时候，耶稣承诺会把他带进天堂。要是中国领导人能够听到这样的福音多好呵，不管他们曾经做过什么，只要他们能够忏悔，他们也同样可以得到我们所拥有的神的爱和宽恕。他们将得到多么大的一件礼物啊？这样他们自己和中国最终都会得到自由！我希望有一天，中国会向南非一样，在曼德拉一样的领导下，通过真相与和解，真正铲除魔鬼通过人的弱点来制造苦难的根源，使中国的每一个同胞姐妹，从贫苦到富足，都真正得到拯救和平安。<br />
<br />
 <br />
<br />
主耶稣的力量是如此伟大，他的胜利是如此的肯定。那就是主复活的意义――那是我以前不能理解的部分，就像我不能理解上帝分开红海解放以色列的做法。因此在所有的考验和困难中，主耶稣的得胜不是“会不会”的问题，而是他决定“何时”以及“怎样”去完成宏伟的计划。因我相信主耶稣就是胜利。<br />
<br />
 <br />
<br />
感谢赞美上帝，我相信中国的兄弟姐妹可以自由无惧的信仰耶稣的那天就要来了。而中国人民和美国人民一样最终将在基督里得到真正的自由！<br />
 <br />
<br />
 <br />
<br />
