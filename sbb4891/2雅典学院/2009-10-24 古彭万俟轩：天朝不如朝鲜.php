																											<div id="postmessage_298387" class="t_msgfont">古彭万俟轩：朝鲜真相让中国精英难以自圆其说<br />
<br />
<br />
<img src="http://image2.sina.com.cn/dy/c/2004-04-21/U397P1T1D3152471F21DT20040421202040.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
<img src="http://news.shangdu.com/301/2009/04/09/images/301_20090409_153.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" />　　<br />
<br />
最近几十年来，中国“精英”们在攻击、污蔑、谩骂毛主席、人民公社、文化大革命的同时，还不遗余力地捎带上古巴、朝鲜等国家。啥原因？因为古巴、朝鲜还在坚持毛泽东时代的社会主义思想和路线。他们把古巴、朝鲜说得一无是处，就是想证明毛泽东时代的社会主义没有出路，只有他们倡导的“特色”路线和私有化进程，才能将亿万人民从“水深火热”的苦海里解救出来。　　<br />
<br />
几十年里，我们得到的朝鲜信息几乎是清一色的：人民面黄肌瘦、神态麻木，街道坑洼弯曲、破烂不堪；汽车没有燃烧的柴汽油，商店货架上空空如也；自然灾害饿死几百万无辜的人民，边界挤满了企图越境逃难的百姓；金家王朝醉心于穷兵黩武，根本不顾人民群众死活；“无赖国家”只想发展核武器，没有心思从事生产、改善群众生活。他们告诉我们的朝鲜根本就不算正常国家，而是一个黑暗的人间地狱。这个国家的人民还生活在暗无天日的“旧社会”，所有现代化的东西都与他们无缘。　　<br />
<br />
可是，最近几条不同信息渠道却给国人送来了另外一个完全不同的朝鲜。这个朝鲜与中国“精英”们污蔑诋毁的朝鲜大不一样，竟是一个幸福、祥和、充满活力的国度。　　<br />
<br />
中国精英们不是吹捧凤凰卫视、凤凰网的报道如何如何公正吗？可是，凤凰网记者卢琛朝鲜之行拍回来的照片，让我们看到的却是令人难以置信的画面。照片上的朝鲜，到处都是喜气洋洋的工人、农民和士兵，满眼都是色彩鲜艳的民族服装，农村公路上到处都是穿梭不断的高档摩托车，乡村鲜花盛开，人民满面笑容，城市里高楼大厦鳞次栉比，街道整洁一尘不染。　　<br />
<br />
难道凤凰网的记者也被朝鲜人民欺骗了？　　<br />
<br />
中国官方媒体不是中国“精英”们的御用传声筒吗？请看中评社在温家宝总理出访朝鲜时发回来的报道吧。报道说，朝鲜软件很先进，平壤家家都有电脑。朝鲜领袖金正日还嘲笑电脑盲是“21世纪三大傻瓜”之一。这句话说得底气十足，因为电脑在朝鲜十分普及！一位朝鲜人说，他天天都在用电脑，或玩游戏，或处理文件。记者还了解到，在平壤买一部电脑，价格从100美元至1000美元不等。朝鲜致力于在电脑科技上“自给自足”，除了拥有自己的广域网，还研发了先进的操作系统、文字处理软件和语音识别软件，计算机编程已达世界先进水平。最令人惊叹的是，朝鲜自行研发的围棋软件，曾连续三届（2003至2005年）在世界电脑围棋赛上，击败美国、德国和日本，获得冠军。　　<br />
<br />
难道官方御用媒体也在一夜之间被朝鲜官方“贿赂”了，或记者收受了朝鲜方面的“封口费”？　　<br />
<br />
另外一条消息更让国人瞠目结舌：朝鲜实行“五大福利，国人自叹弗如。这个消息也是不久前由官方媒体记者透露的。哪五大福利呢？免费实行国民教育；免费实行公费医疗；免费实行住房分配；免费实行养老退休；免费实行工作安排。　　<br />
<br />
具体内容就是：　　<br />
实行全民11年制义务教育。在这11年中，所需的学杂费、文具费由国家负担。高中毕业之后，大约50％的高中生可以升入大学，国家给大学生发放助学金。　　<br />
朝鲜从1953年起实行免费医疗。看病不花钱，老百姓从来不知道手术费多少钱，药费多少钱，因为都是免费的。朝鲜目前的平均寿命是男73岁，女77岁，比中国还高。　　<br />
朝鲜住房全由政府、单位提供，城市居民结婚，可以分到一套　80平方米　以上的住房。农村房屋也由国家统一修建。不收房费，电、水、煤气、暖气也都是统一供应。　　<br />
朝鲜的所有公民退休后领取退休金。女性55岁、男性60岁退休。退休金按每个人贡献和工龄确定，优秀劳动者的退休金拿原工资的100%，最低的也在40%以上。　　<br />
没有考取大学的人，有的参军，有的参加工作。朝鲜的大学教育相当普及，所以朝鲜人文化素质很高。大学毕业之后，由国家统一安排工作。朝鲜没有失业，每个人都有活干。　　<br />
…………　　<br />
<br />
是啊，这些东西让国人越看越不是滋味。几十年来，朝鲜并没有像我们那样，天天吆喝着发展经济，天天吆喝着GDP又提高了多少，他们在社会主义思想路线指引下，悄没声息地独立自主地建设着自己的国家，竟取得了让中国自叹弗如的骄人成就。这才是真本事啊。　　<br />
<br />
看了这些信息，我们在为朝鲜感到骄傲的同时，也为中国“精英”们多年信口雌黄、胡说八道感到脸红。怎么他们吹嘘了多年的改革开放，很多方面还不如“人间地狱”的朝鲜呢？朝鲜真相的披露让中国“精英”们再也无法自圆其说。我现在所担心的就是：面对上述难以辩驳的事实，中国“精英”们如何向国人交代呢？　　<br />
<br />
其实我的担心是多余的。中国精英们最大的本事就是造谣，他们会把白的说成黑的，把黑的说成红的，而且说谎时一点都不脸红。说不定他们会说，上面的那些东西其实还是假的，是为配合国内国际形势需要，逗你玩呢！　　<br />
