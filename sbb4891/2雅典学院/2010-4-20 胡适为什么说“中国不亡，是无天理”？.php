													<h2>胡适为什么说“中国不亡，是无天理”？</h2>
						
						
																											<div id="postmessage_572198" class="t_msgfont">自互联网普及一来，新词层出不穷，其中一个词叫“愤青”。愤青，本来有年龄时间限制――往往指“小青年” 段。可后来人们发现，这个世界上，“愤青”一词并非专为年轻人设计的“头衔”，而只要符合“愤”的标准，都可送上这样一顶“桂冠”，也不管其青与不青。于是，仅就我所知道的，像画家陈丹青、北大教授李零等都获得过这个“称号”。而在自己眼里，“愤青”也并非什么贬义词。至于网上称之为所谓“左愤”们，在本人看来，简直不值一提，故不在本文所议范围之内。<br />
<br />
　　其实，可以称得上二十世纪中国最大的两个“愤青”，一个是鲁迅，另一个是胡适。不用说，大家都知道，鲁迅“愤青之语”多多，别的不说，单说有教授就认为鲁迅不该在《狂人日记》这篇小说中说他在灯下翻看中国历史，居然从字缝里看到的，满篇都只写着两个字，这就是：吃人。是啊，一部中国历史，怎么可能说只会是“吃人”二字呢？可这位聪明的教授忘了，鲁迅这是在写小说。这是一种艺术。艺术是允许夸张的，甚至有时夸张到天上去都可以，且不说鲁迅这种艺术还恰恰是建立在中国人真实的感受之上。别看19世纪末二十世纪初，美国属于“万恶的资本主义社会”，可如果有哪个小说家当时也像鲁迅这样，说他翻看美国历史，从字缝里看到的也只有“吃人”两个字，那就不是艺术，而是胡说了。另外，尽管我们的时代与鲁迅所处的社会多少有些隔膜，但只要看看鲁迅当年这篇诅咒中国历史的小说出来之后人们的欢欣程度就不难明白，鲁迅这句“雷人”的话绝非无的放矢，而是建立在真实的无数“吃人”的中国历史大背景之上。<br />
<br />
　　鲁迅不说了，这里要说的是胡适。如果要说“愤青”的程度，并且还非要作个对比，那么，在本人看来，无论鲁迅对中国传统文化是如何诅咒，乃至要废除汉语，都不及胡适的深度。可以说，若是仅就一个中国人而言，依自己的孤陋寡闻，再没有什么人比胡适的话更“雷人”的了。难怪从互联网上看到，当有人读了胡适的《信心与反省》后，也“惊异于‘愤青’原来这么有历史”。<br />
<br />
　　那么，胡适又说了什么呢？胡适有这样两句语录，一句是：“中国不亡，是无天理。”另一句：“今日的大患在于全国人不知耻。”别的一切不说，只从这两句话中，即可体会出胡适当年对中国有些传统文化尤其是对中国政府乃至中国社会是何等失望，而胡适说这两句话――特别是说头一句话时，大约仍可看作“愤青”的年龄（不过30岁）。当然，就是在说过这句“中国不亡，是无天理”的话后又过了十几年，已过不惑之年的胡适依然不认为有什么不该，只稍作了一点解释。所以说，按现在“标准”，胡适完全可以称得上一个“老愤青”了。<br />
<br />
　　闲话少说，下面就让我们来看看，像胡适这样有理性之人，为什么会说出即使在今天也要被看作是“愤青之言”的“雷人之语”。<br />
<br />
　　先说一九三四年六至七月，胡适一连作了三篇“信心与反省”，即《信心与反省》、《再论信心与反省》、《三论信心与反省》。在本人看来，中国的统治者乃至中国社会，什么时候真正读懂了胡适的这三篇文章，什么时候才能有大进步。而胡适这三篇文章，一言以蔽之：“我们的民族信心必须站在‘反省’的唯一基础之上。”可见，没有反省，也就谈不上什么民族信心。像摩罗这种二十年前的所谓“民间思想家”，今日忽然搞什么“转型”，高喊“中国站起来”，真让人觉得好笑。<br />
<br />
　　上面所举胡适那两句雷人之语，恰恰在《信心与反省》一文中都可读到，我们现在即使把它专门挑出来说，也算不得怎样地“断章取义”。只因要堵一堵那些“五毛”、“左愤”们的口，来比较完整地看看在这两句话的前后胡适是怎么说的：<br />
<br />
　　“可靠的民族信心，必须建筑在一个坚固的基础之上，祖宗的光荣自是祖宗之光荣，不能救我们的痛苦羞辱。何况祖宗所建的基业不全是光荣呢？我们要指出：我们的民族信心必须站在‘反省’的唯一基础之上。反省就是要闭门思过，要诚心诚意的想，我们祖宗的罪孽深重，我们自己的罪孽深重；要认清了罪孽所在，然后我们可以用全副精力去消灾灭罪。寿生先生引了一句“中国不亡是无天理”的悲叹词句，他也许不知道这句伤心的话是我十三四年前在中央公园后面柏树下对孙伏园先生说的，第二天被他记在《晨报》上，就流传至今。我说出那句话的目的，不是要人消极，是要人反省；不是要人灰心，是要人起信心，发下大弘誓来忏悔；来替祖宗忏悔，替我们自己忏悔；要发愿造新因来替代旧日种下的恶因。<br />
<br />
　　“今日的大患在于全国人不知耻。所以不知耻者，只是因为不曾反省。一个国家兵力不如人，被人打败了，被人抢夺了一大块土地去，这不算是最大的耻辱。……一个国家有五千年的历史，而没有一个四十年的大学，甚至于没有一个真正完备的大学，这是最大的耻辱。一个国家能养三百万不能捍卫国家的兵，而至今不肯计划任何区域的国民义务教育，这是最大的耻辱。”<br />
<br />
　　胡适虽然并不把一个国家兵力不如人，被人打败后，还被人抢夺了一大块土地算作最大的耻辱，可读胡适日记发现，他之所以能说出像“中国不亡，是无天理”、“今日的大患在于全国人不知耻”这样的愤青之言雷人之语，与“一个国家能养三百万不能捍卫国家的兵”这种奇怪的现象又有着很大的关系。<br />
<br />
　　远的背景不说。且说从2010年3月下旬到4月上旬初，央视十频道《探索・发现》栏目播出一部历史纪录片，题为《燃烧的黄龙旗》，共十集。可以这样说，不分国籍，不分种族，也不需要什么特别的知识，只要是一个智力正常的人，观看此片，都不难明白堂堂一大国之所以会被弹丸小国打得“满地找牙”，也不难发出像胡适那样“中国不亡，是无天理”的感叹。然而，签订一次次丧权辱国条约，甚至战败于八国联军（慈禧实则向十一国宣战）后，列强们竟要我们赔偿 4.5亿两白银，还说：“中国四亿五千万人，人均一两，以示侮辱。”――按说，这是怎样的奇耻大辱啊，只要稍有一点羞耻之心稍有点正义感的政府，在不得已回电“应准照办”之后，也应该学习两千多年前的那位越王勾践：卧薪尝胆，发奋图强，报仇雪恨。然而，历史告诉我们的是：满清政府毫不知耻。<br />
<br />
　　当然，只是清政府吗？民国政府、国民政府，乃至后来的什么政府，表现得又如何呢？<br />
<br />
　　我不知道现在的中国人在日本人面前是如何表现的，可我想得出，日本人打骨子里一定瞧不起中国人。别看直到今天，中国人嘴一张，还是“小日本”，甚至干脆说他们就是中国的“种”。然而，人家早已脱胎换骨，甚至还很可能改变了自己的某些基因。人家肯学习肯模仿，真正是“谁说得对就照谁说的办”。明治维新，人家发现自己不如人，干脆“脱亚入欧”，尤其在政治制度方面几近“全盘西化”。就连书报检查，也是仿照西方民主国家的新闻制度，于1869年就废止了预防检查制。就这一点而言，比我们已经先进了近一个半世纪。而我们呢，几个当时的“左愤”们却坚持什么“中学为体，西学为用”，说白了，就是要拒绝人家的先进政治制度，只想学一点科学技术。有苍天作证，就这八个字，注定了中国必败！大清必亡！电视片中一再向人们诉说，前方将士流血牺牲，紫禁城的慈禧却在大办寿宴，真个是“前方吃紧，后方紧吃”；日本的皇妃拿出私房钱捐款买军舰，中国的西太后却挪用天文数字的海军军费建造她游玩的御花园。我不相信宇宙背后还有一个什么至高无上的神，但我相信，即使有这么一个神，也一定会站在日本一边。<br />
<br />
　　可怜就可怜了那些将士们，他们不知道，从自己生下地那一刻起，就没有人的尊严，只能是“大清国”的奴隶；他们更不知道，他们所看到的“大清国” 其实早已腐朽烂透，他们的生命随时都可能因为腐败无能的朝廷和落后的政治制度而消失。<br />
<br />
　　可怜就可怜了中国人民！据《燃烧的黄龙旗》第九集片中解说，日本军队不费吹灰之力占了旅顺口后，日军大本营为了给满清政府足够的教训和震慑，旅顺占领军四天之内只干了两件事：屠杀中国男子，凌辱并屠杀中国妇女！就这样，中国两万多和平居民失去生命。不仅如此，在之后的50年内，这一惨相不断在台湾、济南、南京以及二战时中国所有沦陷区内复制、上演、升级。面对这一次又一次奇耻大辱，一朝又一朝的统治者仍不思改革政治制度。我们在观看上世纪四十年代拍摄的影片《一江春水向东流》时，不仅像剧中主要人物张忠良一样根本感觉不到后方的“抗战气氛”，而且还在剧中人物手中的报纸上不也同样看到赫然印着 “前方吃紧，后方紧吃”的大标题吗！这除了无耻除了腐朽烂透，又能作何解！<br />
<br />
　　进入二十世纪，中国的恐日心态依旧很严重，从国民到士兵，从士兵到将军，再从将军到蒋总统。其中虽也有可歌可泣，然看了之后也只会让人感到心酸。别的不说，我们来看看胡适在他的日记中都记下了什么。<br />
<br />
　　1931年9月19日，也就是九・一八之后的第二天：<br />
<br />
　　“今早知道昨夜十点，日本军队袭攻沈阳，占领全城。中国军队不曾抵抗。<br />
　　“午刻见《晨报》号外，证实此事。<br />
　　“此事之来，久在意中。八月初与在君都顾虑到此一着。中日战后（闵按：指1894年甲午战争），至今快四十年了，依然是这（样）一个国家，事事落在人后，怎得不受人侵略！”⑴<br />
<br />
　　1933年3月2日：<br />
<br />
　　“……晚上到张学良将军宅吃饭，他说，南R已失了。他说，人民痛恨汤玉麟的虐政，不肯与军队合作，甚至危害军队。……<br />
　　“我忍不住对他说：事实的宣传比什么都更有力。我们说的是空话，人民受的苦是事实，我们如何能发生效力？最后［好］是你自己到热河去，把汤玉麟杀了或免职了，人民自然会信任你是有真心救民。<br />
　　“我对他说天津朋友看见滦东人民受的痛苦，人民望日本人来，人心已去，若不设法收回人心，什么仗都不能打。<br />
　　“……<br />
　　“张将军只能叹气撒谎而已。<br />
　　“国家大事在这种人手里，那得不亡国？<br />
　　“十几年前，我曾说，‘中国不亡，是无天理’。今日之事，还有何说！”<br />
<br />
　　再看3月4日：<br />
<br />
　　“今日下午三时，在后援会得知日兵已入承德，汤玉麟不知下落，人民欢迎敌军。<br />
　　“自朝阳到承德凡二百英里，日兵孤军深入，真如入无人之境。……”<br />
<br />
　　3月5日又记：<br />
<br />
　　“昨日进承德的日本先锋队只有一百廿八人，从平泉冲来，如入无人之境！”<br />
　　128个日本兵居然有这种“威力”，说白了，正是中国军队、中国政府给的。一个国家到了人民居然盼望侵略者来，侵略者又怎能不嚣张！这种国家又还能有什么指望！<br />
<br />
　　3月13日：<br />
<br />
　　“……<br />
　　“五点，见蒋介石先生，谈了两点钟。<br />
　　“他自认实不料日本攻热河能如此神速。他估计日本须用六师团人，故国内与台湾均须动员。‘我每日有情报，知道日本没有动员，故料日本所传攻热河不过是虚声吓人而已。不料日本知道汤玉麟、张学良的军队比我们知道清楚的要多的多！’<br />
　　“这真是可怜的供状！误国如此，真不可恕。<br />
<br />
　　“我们问他能抵抗否，他说，须有三个月的准备。<br />
<br />
　　“我又问：三个月之后能打吗？<br />
　　“他说：近代式的战争是不可能的。只能在几处地方用精兵死守，不许一个人生存而退却。这样子也许可以叫世界人知道我们不是怕死的。<br />
　　“其实这就是说，我们不能抵抗。<br />
　　“我们又说：那末能交涉吗？能表示在取消‘满洲国’的条件之下与日本开始交涉吗？<br />
　　“他说，我曾对日本人这样说过，但那是无效的。日本决不肯放弃‘满洲国’。”<br />
　　在此时的胡适眼中：汤玉麟、张学良、蒋介石，皆“误国”。<br />
<br />
　　读胡适1935年7月5日（胡适误记为“六，五”）日记，想凡有良知者都会与胡适一样说出那句“伤心的话”，即“中国不亡，是无天理”，也没感到有一点过分――尽管当时已经牺牲了很多抗日将士，而且有不少士兵都还是二十岁以下，还有几个可怜不过十八岁：<br />
<br />
　　“早晨八点多，白映星、张锡羊诸君来车上，同我们到第三十五军军部，傅主席加入，和我们同去看大青山‘抗日阵亡将士公墓’。墓地离车站不远，在车上可望见墓碑塔。<br />
　　“墓碑［文］是我做的，钱玄同写的，这是第一块白话碑文用全副新式标点符号分段写刻的，所以拓本流传全国。我曾说：‘这碑不久会被日本毁灭的。’但我不曾想到日本人还不曾占据绥远，我的碑已被‘埋葬’了！”<br />
<br />
　　那么是谁这么毫无心肝呢？接着读同一则日记，胡适是这样告诉我们的：<br />
<br />
　　“当上月华北形势最危险时，小心的何应钦将军打了几个电报给傅宜生（闵按：即前面提到的傅主席，即傅作义），叫他消灭一切‘抗日’的标帜，尤其是这里的阵亡将士公墓。傅不得已，把塔上‘抗日阵亡将士公墓’的‘抗日’二字挖成了‘长城’二字，挖改的痕迹尚可认识。我的碑文也蒙上了一层沙石，另刻上了‘精灵在兹’四个大字。全国送来的匾、联、铭、赞，凡有刺激性的，都设法迁毁了，只剩林主席的‘河山壮气’一个匾。我站在这二百零三个国殇的墓前，真不胜感慨。<br />
<br />
　　“塔与墓之间，建有祠堂，中设阵亡将校兵士的照像，各有履历，有许多兵士在二十岁以下的，有几个不过十八岁。<br />
<br />
　　“碑塔是钢铁作骨的，深入地中。据傅主席说，这塔是不容易炸毁的。”<br />
<br />
　　历史又过去了75年。75年后，容我接着傅作义的话说，中国的民心都炸毁了，一个塔“不容易炸毁”又还有多大用呢！既然面对侵略者的暴行屠杀连有一点“刺激性的”话都不敢说，委曲求全到如此地步，还有何骨气，还谈什么“抗日”，这种国家还有何面目存于世上！读到我们这种“历史情形”，岂是胡适 “不胜感慨”四个字可以“感慨”得了的。前方将士不畏强暴，英勇杀敌，最后牺牲了自己的生命，然而，连“抗日”二字也要从墓碑上挖去，恕闵某人孤陋寡闻，这种“现象”在人类史上不知可曾出现过第二次！挖去了“抗日”二字，岂不愧对那躺在地下的二百零三个英灵！他何将军乃至全军将士又还有何面目面对世人！什么“河山壮气”，我说应该叫“河山哭泣”！倘若英灵们地下有知，他们难道不会发出像胡适那样的诅咒吗！有何应钦这样的“将军”，还不可以说“中国不亡，是无天理”吗？何将军的电话是何等长侵略者志气，又是何等灭中国政府中国士兵自己的威风啊！<br />
<br />
　　此外，在“九・一八”三周年到来前一天，胡适还作了一则不过500字短文，题为《“九・一八”的第三周年纪念告全国的青年》。他在短文中强调： “我们应该最诚恳的反省”。反省什么呢？这就是：“为什么我们把东北四省丢了？是不是因为我们自己太腐败了？是不是因为我们自己太不争气了？是不是因为我们自己事事不如我们的敌人？”“我们口头和笔下的纪念都是废话，我们的敌人不是口舌纸笔所能打倒的，我们的失地也不是口舌纸笔所能收回的。”“我们的唯一的生路是努力工作，是拼命做工。我们的敌人可以能够这样侵犯我们，欺负我们，只是因为他们曾经兢兢业业的努力了六十年，而我们只在醉生梦死里鬼混了这六十年。现在我们懊悔也无用了。只有咬紧牙根，努力做我们必须做的工作。”胡适在短文结尾处写道：“奇耻在前，大难在后，我们的唯一生路是努力，努力，努力！”⑵然而，中国是如何努力的，蒋介石又是如何努力的，后来的政府又是如何努力的，后人们都看到了。<br />
<br />
　　说到这里，本文本该作结。只是我们知道，胡适一生的愤青之言雷人之语当然不止这些，有些话有些文字的分量甚至并不亚于这两句。比如，早在 1918年，胡适就作过一首白话诗《“你莫忘记”》，很是有名。诗前有一小序：“此诗作于六月二十八日，当时觉得这诗不值得存稿，所以没有修改他。前日读《太平洋》中劫余生的通信，竟与此稿如此一口，故又把已丢了的修改了一遍，送给尹默、独秀，玄同、半农诸位，请你们指正指正。”<br />
<br />
　　诗是这样写的：<br />
<br />
　　我的儿子，/我二十年教你爱国，――/这国如何爱得！……/你莫忘记，/这是我们国家的大兵，/强奸了三姨，逼死了阿馨，/逼死了你妻子，枪毙了高升……/你莫忘记，/是谁砍掉了你的手指，/是谁打死你的老子，/是谁烧了这一村，/嗳哟！……火就要烧到这里，――/ 你跑罢！莫要同我一起死！/回来！/你莫忘记：/你老子临死时只指望快快亡国；/亡给“哥萨克”，亡给“普鲁士”――/都可以，――/总该不至――如此……⑶<br />
<br />
　　细心的读者可能已经感觉到，这首诗中有些文字比“中国不亡，是无天理”更“愤青”更“雷人”，而那句“你老子临死时只指望快快亡国”，可以说，与“中国不亡，是无天理”简直不就是一个“腔调”吗？<br />
<br />
　　2010-4-3<br />
<br />
　　注释：<br />
<br />
　　⑴见44卷本《胡适全集》第32卷第149页，安徽教育出版社2003年版。后面所引此卷文字，因所引日记皆有日期，极易查对，故不再另注页码。<br />
　　⑵见胡明主编16卷本《胡适精品集》第12卷第313～314页，光明日报出版社1998年版。<br />
　　⑶见7卷本《胡适文集》第1卷第219～220页，人民出版社1998年版。<br />
<br />
　　作者：闵良臣，原载：共识网</div>

							
							
							
							
