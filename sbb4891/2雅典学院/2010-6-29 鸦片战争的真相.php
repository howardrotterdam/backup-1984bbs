																											<div id="postmessage_711798" class="t_msgfont">韩毓海：鸦片战争的真相<br />
<br />
&nbsp; &nbsp; 实际上，大清朝有很长的向西方开放的历史，这一历史绝不起自鸦片战争。诸欧洲国家中，大清尤其重视的是沙俄，因为只有俄罗斯才能从西北两个方向上威胁中国的腹地。大清一方面必须警惕地来自北方的威胁，另一方面则要与沙俄建立起和平稳定的关系，这从而形成了中国外交的一个历史传统，其中尤以康熙时代特别突出。<br />
&nbsp; &nbsp; 我们都知道康熙在雅克萨之战中将沙俄饱揍了一顿，但很少知道康熙对于沙俄还有积极发展经贸关系这另外一手。一手硬，一手就必须软，这个其实也是平衡。在这个意义上，不但北京是个重要的国际贸易城市，康熙实际上更是一位热衷于改革开放大业和对外经济贸易活动的君主。<br />
&nbsp; &nbsp; 前面提到的法国人白晋，在给路易十四所写的秘密报告中曾经惟妙惟肖地指出，当年莫斯科的使者一踏上大清的土地，就受到康熙特使的全程陪同热情招待，“北京是皇帝允许莫斯科人自由通商的大城市，在通商过程中，他们无须交税，更不会受到欺凌。皇帝这样做是为了使他们有利可图，以便永远保持通商的睦邻关系。”而且，“莫斯科人不是惟一因受到中国皇帝的款待而感到满意的外国人。皇帝也以其特有的伟大胸怀热情款待了荷兰、葡萄牙来华的使节。”白晋特别指出，对于这些外国人，康熙并不要求他们按照朝贡之礼给自己下跪，“皇帝根本不愿意外国人遵循中国那一套谦卑的礼仪，他亲切地对待外国使节，包括拒不履行中国礼节的粗野的莫斯科人，令后者大为感动。”<br />
&nbsp; &nbsp; “在通商过程中，他们无须交税，更不会受到欺凌。皇帝这样做是为了使他们有利可图，以便永远保持通商的睦邻关系。”DD白晋记述的这一段其实是非常关键的，因为这正是大清鼓励自由贸易的铁证。而且他还特别强调指出：清朝贸易收税很低，甚至几乎不收税，即大清不以贸易为税收的主要来源，这恰恰也是清代贸易发达的基本原因。而注意这一特征，对于我们了解清朝时候的中国，特别是它与英国在对待贸易问题上的区别，乃是至关重要的。<br />
&nbsp; &nbsp; 首先，按照这位深入中国宫廷的法国学者、皇帝的科学顾问和教师的叙述，我们可以推断，所谓康熙的孙子乾隆皇帝虚荣无知，因英国人不肯下跪就不愿意与英国通商的说法，不过是某些心怀恶意的历史故事大王无聊的编造，完全不符合当时的历史事实。<br />
&nbsp; &nbsp; 真正的历史事实其实是这样的：1793年的马噶尔尼使团在被盛情招待后，提出的竟然是如下与“贸易”毫不相干的要求：一、请于舟山附近划一不设防之岛，归英国商人使用，以便英国商人休息、存放一切货物、且永久居住。二、请于广州附近得一同样之权利，且听任英国人自由来往，不得加以禁止――而这其实也就是割地。乾隆皇帝理所当然地回绝了这一要求，也正是根据这一无礼要求。乾隆才判定了英国人的野蛮和无知。在给英王的敕书中，他这样义正词严地教育英国人：“天朝尺土，俱归版籍，疆址森严，即岛屿沙洲，亦必划界分疆，各有专属――此事尤不便准行。”<br />
&nbsp; &nbsp; 毫无疑问的是，乾隆对于他国在东南沿海的任何动作显然均存戒心，这种戒心一开始未必就是针对英国人，甚至也不是针对任何外国人的，更不是针对贸易的。大清对于海洋边疆的戒心，其实主要是由于长期的台湾问题造成，是出于领土的原因。正是为了防止台湾郑氏王朝的袭扰，康熙才发布了禁海令。由于当年台湾曾得到荷兰海军的军事援助，故乾隆时代，大清理所当然地依旧警惕地注视着外国势力干预台湾的可能性，防止那里再次成为任何人进攻大陆的海上根据地。因此，关于那次著名接见，首先也是尤其必须澄清的一点是：乾隆并非拒绝海上贸易，而是不允许开放一个海岛，作为英国商人的货物贮藏地和战略根据地。所谓兹事体大，这事涉关领土，而不是贸易，而这恰恰说明了他的精明警觉、深谋远虑。<br />
&nbsp; &nbsp; 乾隆并非只会读书写字的太平皇帝，他是在炮火连天的战场上锻炼出来的鹰派军人。在严峻的边疆挑战中，乾隆经历了无数艰难险阻才将中国历史推向鼎盛。西藏、新疆问题他从来没犯过糊涂，中国西南、西北边疆的安定是他数次亲征，率领自己的子弟兵浴血奋战保下来的。崽卖爷田不心疼，我们今天批判历史上的专制主义，但并不意味着要反过来为帝国主义的侵略辩护，更不意味着全面割断历史，好像中国历史5000年，除了“专制”二字以外什么也没有，清朝更是除了辫子别无长物。说白了，我们今天的版图就是大清挣下的；乾隆一生起码没有丢过一寸中国领土，而是扩大和保卫了中国的版图。对他而言，肯定是丢掉一寸领土都肉疼，宁舍金银不卖地，这朴实的农民性格，其实也就是大陆统治者的底线。仅凭这一点，给他个民族英雄的称号也不为过。<br />
&nbsp; &nbsp; 把乾隆描绘为谁给他糖吃、谁给他磕头他便高兴的弱智儿童，只能说明“故事大王们”歪曲历史的水平太低，到了连谎话都编不圆的程度，况且谎言重复一万次也不等于真理。乾隆当年并不在乎马噶尔尼是否给他磕头，何况乾隆拒绝马噶尔尼的根本也不是贸易，而是拒绝给英国一个岛作货物集散地，这一条在历史上白纸黑字、清清楚楚，容不得歪曲。而乾隆这种考虑，其实完全是基于台湾的经验，基于战略原因、领土安全作出的。历史证明，他的考虑实际上是十分正确的，即使在今天，我们依然不能允许香港和台湾成为颠覆大陆的根据地，也就是出于这个原因。<br />
&nbsp; &nbsp; 值得强调的历史事实是：早在东南海禁未开之前，大清就有开放口岸之举，而且开放的还是与台湾郑氏军事对峙的厦门（见《闽海纪要》），好比今天的“小三通”。康熙23年（1684年）海禁止，大清即开澳门、漳州、宁波、江苏云台山为四个通商口岸，随后又开广州、厦门、上海通商（见戴逸《简明清史》）。尤其重要的是，在广阔的陆地边界，大清一直开放着大规模的国际贸易。近代中国的研究中，一个说来可笑，却从来就没有被解释过的问题是：是否只有中国向英国开放才算是开放？只有海洋贸易才是贸易，而陆地上的贸易数额再大，它也不是贸易？按照这个糊涂逻辑，那么欧洲大陆上的贸易，乃至南欧与北欧之间、欧洲经奥斯曼帝国与阿拉伯世界之间就从来没有存在过贸易？如果真是这样，那从威尼斯共和国到荷兰，资本主义在欧洲发迹的历史岂不就一笔勾销了？！而且，荒唐的恐怕还不仅仅是这个，更在于――我们是否完全忘记了：当年坚决不同意与英国进行海洋贸易的，不仅仅是咱们大清一家、乾隆一人，而是包括世界上几乎所有伟大的国家及其英明君主――在欧洲有荷兰，在荷兰之后有美国、法国，特别是19世纪后期以来，提出了一整套反所谓自由贸易的政治、经济、文化政策的德国。他们全都曾经比咱大清更为彻底而坚决地反对过英国的自由贸易和海洋贸易，而且这些国家都曾经或者依旧是世界领袖、霸权豪杰，难道他们坚决不跟英国做生意就是英明正确，偏偏大清不跟英国做生意就成了罄竹难书的罪恶？难道反对英国就等于反对贸易，拒绝英国就等于拒绝文明、拒绝现代化？这种滑天下之稽的说法，我想大概只能产生于20世纪后期中国极其荒谬的后殖民语境中。这是非常值得我们反思的。<br />
&nbsp; &nbsp; 其实，清朝不但从来没有一般地反对过国际贸易，它一直与沙俄保持着长期的贸易关系。而且这种贸易关系，也并不仅仅是出于单纯的国家税收考虑，因为它一直是修好北方邻邦，稳定大清西北边疆的重要方式。<br />
&nbsp; &nbsp; 早在与英国进行大规模贸易之前，大清与沙俄就在恰克图这个地方进行巨大的贸易活动，这恐怕是近代中国历史上规模最为庞大的国际贸易。正如白晋所说，这种贸易的税收还非常低。也正因为大清并不把贸易作为国家税收的主要来源，因此才使得商人在贸易中获得了最大的利益――仅就这一点而言，就能说明大清并非是闭关自守的，更不能说它是拒绝自由贸易。<br />
&nbsp; &nbsp; 但是，大清与英国对待贸易的态度的确又是完全不同的。恰恰是这种态度和原则的不同，才成为日后中英在贸易问题上发生尖锐冲突的关键原因，也成为揭开近代历史的重要线索。<br />
&nbsp; &nbsp; 最简单地说，这种原则和态度的区别就在于国家应该在贸易中扮演何种角色问题。换句话说，国家是否应该将自己等于贸易公司的保安队长，或者市场上的黑社会老大――靠收取商贸“保护费”发财？所谓美妙的“自由贸易”，是否说白了就是指这个？<br />
&nbsp; &nbsp; 换句更为学理点的说法：国家是否应该将对外贸易当作政府税收的主要来源？当国家将外贸视为税收主要来源的时候，自由贸易是否就会自然而然地得到鼓励、得到发展？当然，这一点在当今所谓自由主义拜物教者那里就是铁定无疑的，而这些人说的不好听，好像天生就是作毒品买卖或者当鸡婆的――还是把话说白了，因为只有这两种买卖才必然需要保护伞，天下只有做毒品买卖或者当鸡婆的，才必然以为只要有了黑保安、交了“保护费”，它就可以想干什么就干什么，可以堂而皇之地把贩毒、卖淫当正当生意了。然而，正是从历史上看，这种看法简直就是滑稽之至、荒谬绝伦。<br />
&nbsp; &nbsp; 从事近代历史研究的人，起码应该知道这样一个基本事实，那就是大清的关税与它的其他税收一样，乃是普天之下最低的――低到了什么程度呢？简单的数字可以说明一切：康熙时规定的关税“正额”只有银4万3千两，而且直到鸦片战争时并无改变！<br />
&nbsp; &nbsp; 随着贸易的迅猛发展，政府虽然也增加了“盈余”，可是这些“盈余”与贸易额的比例却从来也没有突破过9％。如19世纪30年代，广州的贸易价值为1700万两的规模，国家的盈余最多也就是150万两！根据马士在《中华帝国对外关系史》中的比较，就茶叶这一项来算，中国政府的税收加上官员的贪污，最多也不会占总贸易额的30％，而仅英国政府对茶征收的进口税就高达90％，是广州发票价格的200％！另一位更老实的英国史家格林堡的《鸦片战争前中英通商史》中更坦率地指出，英国人总是贬低中国官员之贪污，但忘了说明英国当时远非清廉之国。实际上，英国的腐败远在中国之上，中国官员“勒索的总数，与东印度公司每年从对华贸易销货所得中付给英国国库、特别是债券持有人的几百万英镑，那简直是难以比拟的”。<br />
&nbsp; &nbsp; 以收取贸易“保护费”为目的的英国政府和以放高利贷为目的的英国股票商人――黑社会打手和高利贷者DD正是这两部分人共同构成了某些洋务派人士眼里的“先进文明”的代表。而这如果不是“认贼作父”，那又是什么呢？<br />
&nbsp; &nbsp; 首先，这种“收保护费”的保安队，不但对于中国商人，而且对于英国商人，乃至对于天下所有老老实实做正经生意的人而言，其实还不如没有的好。以英国商人最为关心的布匹出口这一条来说，在英国费时费力好不容易把中国打到了鸦片战争的谈判桌上后，一个喜剧性的事实竟然是：中国在东南沿海以不平等条约的形式确定的与英国进行的“布匹贸易”这一项，还是大大低于帝国同时与俄国在西藏和北方的恰克图所进行的布匹贸易额，为什么如此？因为英国鼓吹的所谓“自由贸易”，其实不过是国家提高税收的手段罢了；这种“国家拿大头，商人得小利”的“自由贸易”，具有深刻的重商主义――也就是收保护费的色彩；这一原则对于正当的商人和贸易，其实根本就没有什么好处――尽管国家得到了税收，但是羊毛出在羊身上，商人和贸易却背上了沉重的税务负担。而与此不同的是，长期的中俄双边贸易，却是国家低税收的贸易。对于大清而言，这种贸易既有商业的考虑，但更是出于建立睦邻关系，稳定边疆的安全目的。因此，为了鼓励这种商业关系，国家对于这一贸易的税收一直保持很低。而这样一来，国家的低税收自然也就对商人造成鼓励，从而对贸易产生了推动。一旦利润丰厚，商人赚钱的积极性高涨，贸易额自然就直线上升。<br />
&nbsp; &nbsp; 因此，由英国通过鸦片战争确定下来的“贸易条约制”，其实并不是什么保证自由贸易的制度。说白了，这不过就是保证国家“向贸易征税”、收保护费的制度。这种高额保护费，对于黑生意当然是保护伞，而对于正常的生意而言，那完全就是剥削。而这一点微妙却是根本性的区别，恰恰被革命导师恩格斯注意到了。也就是说，不是中国的历史学家，而恰恰是恩格斯这个英国企业家的儿子，以他精明的商业头脑看出来：中英鸦片战争缔结的条约，相对于天下所有老老实实做生意的商人而言，那才是“不平等条约”呢！<br />
&nbsp; &nbsp; 为什么这么说呢？因为鸦片战争订立的贸易协定，其目标不过是“便利英国国家征收贸易税”，而不是为了促进任何意义上的“自由贸易”。它实质上强调以“条约”的方式对于贸易进行管理，从而使得贸易成为国家税收的主要来源；同时更以这样的方式，将贸易置于国家的完全控制之下。这种以贸易为名的条约制度，反过来破坏了真正的自由贸易。<br />
&nbsp; &nbsp; 这样一来，所谓的“历史戏剧性”就出现了：如果说中国在西北与沙俄进行的贸易是以国家低税收为条件的，是以市场为核心的“自由贸易”的话，那么恰恰正是英国的条约制，反而破坏了自由贸易。例如为当时的布匹贸易加上了高额的税收。换句话说，英国打了一仗，强迫中国签订了《南京条约》，而根据这个条约，英国的布匹进入中国市场，在加上英国的出口税和中国的进口税之后，却更加无法与俄国布匹竞争了――因为当时中国与俄罗斯的贸易的税率是很低的，如同今天在俄罗斯的中国倒爷们一样，几乎是赚了钱基本归自己，不给国家交钱。如果什么叫自由贸易，这才叫自由贸易呢！<br />
&nbsp; &nbsp; 恩格斯也正是从市场和贸易的视野出发，揭开了英国政府“皇帝的新衣”。他认为鸦片战争的起因实际上是英国政府通过高额税收来管理着鸦片种植和鸦片贸易，这项生意是政府的重要财政来源。因此，如果中国禁烟，那么受损失的就不仅仅是鸦片商人，而更是英国政府的税收。因此，《南京条约》的核心内容其实就是强调中英两国政府都必须通过税收的方式“对鸦片进行管理”。换句话说，这其实是与当今某些时贤们主张把红灯区变成国家税收的基本来源，应该得到鼓励的论调基于一个逻辑、同一付腔调，起码它们之间并没有什么根本上的不同。说白了，这二者其实都不过是让国家承担起“收保护费”的“黑社会”角色罢了。<br />
&nbsp; &nbsp; 这种政策，对于规规矩矩做干净生意的商人，不但没有任何的好处，还等于破坏了他们的根本利益，因为这等于迫使老老实实做生意的商人，与毒品贩子和开妓院的鸡头老鸨们“在同一个起跑线上竞争”――只要他们肯给国家缴税就行。因此，恩格斯这个曼彻斯特工厂主的儿子一针见血地指出：对于一般商人来说，这就是不公正的国家干预，它不仅仅是破坏自由贸易，破坏公平竞争，而且完全是逼良为娼之举。<br />
&nbsp; &nbsp; 从某种意义上说，鸦片战争的核心并不完全在于鸦片。对于英国来说，其最终战略目的实际上有两个：其一，是摆脱广州公行（也就是广州“十三行”）在当时的亚洲贸易体系中所扮演的“关贸总协定”地位，另一个就是要求割让香港作为其在南中国海的根据地――比起这两条，鸦片乃至通商都是次要的。所以林则徐的继任者琦善在1841年1月20日的《穿鼻草约》中仅仅通过“割让香港”和“广州通商”这两条，就使得英国罢兵休战――在他看来简直是“略施小技”就将英国人摆平了。<br />
&nbsp; &nbsp; 清廷之所以让琦善代替林则徐，是由于这位博尔济吉特王公自1824以来在海运、漕运等一系列重大决策方面表现果断，但却万万没有想到他关键时刻如此没有政治头脑，拿大原则――领土作了交易。当年乾隆皇帝没有答应马噶尔尼的割香港，竟然被琦善就这么痛快地一口答应了。道光得到消息后悲痛欲绝，下诏曰：“尺土一民，莫非国家所有，琦善擅与香港，十恶不赦！”<br />
&nbsp; &nbsp; 实际上，也就是为了香港，大清才断然不承认《穿鼻草约》，为此不惜与英国再次开战，最后不得不签订《南京条约》。从长远看，《南京条约》对于中国最大的打击并非赔款乃至五口通商，而是公行制度被废除。与《穿鼻草约》相比，《南京条约》却将香港从永久割让变成了“租借”，而这却是值得的――如果大清当年承认了《穿鼻草约》，那么百年后的今天就将不存在“香港回归”的问题。</div>

							
							
							
							
