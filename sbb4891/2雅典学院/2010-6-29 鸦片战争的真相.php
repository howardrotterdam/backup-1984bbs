十三行问题可以细说，据我的记忆，广州的行商在对外贸易活动中，依靠政府给予的特权，垄断了广州整个对外贸易。十三行大概是一个半官方（由官方授权）的行业工会组织。你知道，当时通商的口岸也只有广州而已，外国商人也不能直接同销售者打交道，而必须通过十三行经手，不说是垄断天理难容啊～<br />
<br />
资料：<br />
<br />
“康熙帝三十八年及五十九年，广州行商曾两次组建公行，但为期都不长。公行议定行规，表面是为约束不法行为，扶持对外贸易，实际上却增加了不少禁约。它对货物实施公行垄断，以便按照行会的利益自行调整价格。英商为打破公行垄断，通常用收买个别行商、贿赂官府的手段，使公行难以持久，如乾隆帝二十五年广州公行正式奉准成立,到三十六年即被解散。此外，公行存在期间，在行商之间及行商和散商之间，又为争夺商业利润互相倾轧，外商得以乘机在进出口货价和交易量上利用矛盾，遂造成公行的亏损和债务；公行制度下的行商，因对行欠债务负有连带责任，故不断出现倒闭。乾隆帝四十七年公行再度恢复，并开始设立利用行佣积累起来的公所基金，用以清偿行商的拖欠、罚款等，以维护公行的稳定。重建后的公行，延续了近六十年。 鸦片战争以后，《南京条约》规定，废除中国对外贸易中的公行制度，允许英国商人在各口岸任意与华商交易。”<br />
<br />
文中把它说成是“关贸总协定”，是比喻失当。</div>

							
							
							
							
