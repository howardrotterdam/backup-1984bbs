																											<div id="postmessage_862636" class="t_msgfont">诺贝尔和平奖又砸自己的牌子<br />
<br />
<a href="http://www.huanqiu.com" target="_blank">http://www.huanqiu.com</a> 2010-10-09 15:56 环球时报 我要评论 <br />
<br />
<br />
　诺贝尔委员会8日将世人瞩目的诺贝尔和平奖给了正在中国监狱里服刑的刘晓波，这个奖项再次把自己摆在了中国人民以及中国改革开放事业的对立面。<br />
<br />
<br />
　　诺贝尔奖总体上在中国是有威望的，但诺贝尔和平奖已成为借“诺贝尔”这个品牌强行塞给世界的西方意识形态的私货。上世纪的这个奖项多次颁给反对苏联的人，包括直接搞垮苏联的戈尔巴乔夫，这样的偏好并未随着冷战的结束而消融。<br />
<br />
<br />
　　通过颁奖否定现代中国，成为诺贝尔和平奖新的偏执狂般的追求。中国迄今两人获奖，一人是达赖，一人就是刘晓波。前者是中国民族分离主义的代表人物，后者是鼓动把西方政治制度照搬到中国，并抵制中国现行法律的人。无论从外部看他们两人可以有多少个视角，但毫无疑问的是，两人都不是中国近年和平与发展的建设性力量，他们对中国社会的触动是通过示范对抗，他们没有在有着种种复杂问题的中国宣扬团结。把以“和平”命名的奖项颁给他们，是对大多数中国人的不尊重，是西方意识形态在中国人面前的一次傲慢展示。<br />
<br />
<br />
　　由于热比娅、胡佳、魏京生等“异见人士”也都进入过诺贝尔和平奖的候选人名单，更加剧了中国人对这个奖项的反感。善良的中国人有理由怀疑，诺贝尔和平奖已经沦为服务于西方利益的政治工具，评定及操纵这个奖项的人，不愿意看到中国的和平、团结，很希望中国社会因政治分歧陷入无穷无尽的纷争，直至走向苏联式的分裂。他们现在能做的就是用和平奖给中国社会撕开个口子。<br />
<br />
<br />
　　刘晓波去年被中国司法当局判处11年监禁，西方多国政府曾因此出面干预，诺贝尔委员会8日的行为是这种干预的继续。西方对刘晓波案的争议不是对司法本身的探讨，而是希望中国现在就推行政治多元化，让中国的发展“西方化”。<br />
<br />
<br />
　　诺贝尔委员会选在刘晓波服刑时给他这个奖，与其说是对他个人的鼓励，不如说是对中国司法制度的蔑视和挑战。每一个中国人都能感觉到这种故作的恶意，西方的大多数公众也能一眼看出这是故意做给中国看的，中国社会与西方的感情隔阂将因此加剧。根据诺贝尔的遗嘱，和平奖应该颁给“为促进民族团结友好、取消或裁减常备军队以及为和平会议的组织和宣传尽到最大努力或作出最大贡献的人”，和平奖委员会昨天却做了相反的事。<br />
<br />
<br />
　　诺贝尔和平奖的反复非难，再次提醒了中国在世界上的处境。中国的发展要获得西方的掌声几乎是不可能的，中国在选择发展道路时需要足够的坚强，西方骚扰我们判断的手段经常比我们可以预见的更多，更出其不意。<br />
<br />
<br />
　　值得一提的是，诺贝尔委员会及他们代表的那些人，终将拗不过中国发展的大势。他们只是让中国生了一回气，却同时又砸了一次自己的牌子，但世界生生不息。那几个少数人终究不能评判世界，过于傲慢的西方也一次次误判中国人民的意志，以及中国的走向。实践是检验真理的唯一标准，诺贝尔奖也没有这个道理大。▲<br />
