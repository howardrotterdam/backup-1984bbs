													<h2>胡星斗：中华民族是劣等民族吗?</h2>
						
						
																											<div id="postmessage_531961" class="t_msgfont">中华民族是劣等民族吗?<br />
<br />
 <br />
<br />
胡星斗<br />
<br />
 <br />
<br />
&nbsp; &nbsp; 偶有外人特别是一些日本人“恶毒攻击”中国人属于“劣等民族”，总是让我心情久久不能平静，甚至愤怒之情溢于言表，欲大骂“日本人才是劣等民族”！君不见日本人之野蛮血腥、刻板机械、天皇崇拜、篡改历史，只会模仿不会创造，哪有一点先进民族的样子？可是静下心来之后理性地想一想，人家能够借助欧美制度发达起来了，成为世界第二大经济实体，即便她有许多民族缺点，也在其次了。而我们中国人，虽然曾经辉煌过，但现在许多根深蒂固的民族劣根性缠绕、羁绊着我们，使我们不肯痛彻地反省自己，不愿意学习他人之根本，终究被他人视为劣等民族了。<br />
<br />
 <br />
<br />
&nbsp; &nbsp; 有人说，从某些方面来看，从历史上来看，中华民族又是“优等民族”。其表现为先秦诸子百家、唐诗宋词之辉煌；最早之封建制度（西周之贵族分封制）、专制制度（商鞅、秦始皇之郡县制等）之“制度创新”；四大发明以及化学、数学、天文学和炼钢术、丝绸、瓷器等实用技术之领先；智慧、宽容、忍让、和平、善良，以及尊师重道、重文轻武、自强不息、厚德载物等民族性之优点。<br />
<br />
 <br />
<br />
&nbsp; &nbsp; 可是又有人说：上述成就和优点都属于过去，现在的中国人有什么值得骄傲的？正像一个德国将军所说的：“我敬佩中国人，但敬佩的是古代中国人！”听罢此言，如今的中国人真该无地自容！<br />
<br />
 <br />
<br />
&nbsp; &nbsp; 不是么？从世界范围来看，如今的中国人，哲学、文学、艺术无可称道；社会的一些方面仍未走出前现代状态；经济虽然已经取得了很大的成就，特别是改革开放以后经济迅猛发展，但总体上看，我们仍不过是世界分工体系中的打工者，主要是发达国家在制定游戏规则，我们参与较低级别的劳动；我国的人口、资源、生态环境岌岌可危；贪污腐败、分配不公、农民问题等社会矛盾未见缓解。与先进民族相比，我们确实还有一段很长的路要走。<br />
<br />
 <br />
<br />
&nbsp; &nbsp; 只是，对德国人的话，我还要补充几句：首先，敬佩古代中国人，应敬佩的是先秦诸子、张衡李白们，应敬佩的是苦难而智慧的中国百姓；至于专制统治者，他们绝大多数残忍而变态，伪装神圣，无恶不作，造成了王朝循环的灾难、无规则的政治、不择手段的社会、不断出现的饥荒、人口常常减半的战争、民族生存环境的大破坏，这些中国统治者确实是劣等人的代表；其次，现代中国也有可敬佩之人，中华民族的优秀子孙――鲁迅、胡适、陈独秀、蔡元培、陈寅恪、顾准，以及年轻时的毛泽东、周恩来，他们勇于批判现实，揭露丑恶，挑战强权，驱赶黑暗，呼唤民主自由，为中国的现代化奔走呼号，他们是中华民族的脊梁，是优等中国人的代表！有了他们，中国才有了希望。特别是在当代，那些改革开放中的弄潮儿、为实现现代化而敢为天下先的中国人，那些以天下为己任、忧国忧民、揭露时弊的当代鲁迅们确是优等人类。<br />
<br />
 <br />
<br />
&nbsp; &nbsp; 所以，虽然中国的专制统治者属于劣等，但中华民族拥有众多的优秀分子。<br />
<br />
 <br />
<br />
(按：本段虽把“劣等”一词限于专制统治者，但统治者也不是天生的，也就是说不是由上天强加在老百姓身上的，他们也是从老百姓中产生的，是经过流血争斗而坐上统治者的宝座的。问题的根源出在哪里？值得我们深思)<br />
<br />
 <br />
<br />
&nbsp; &nbsp; 判断一个民族优劣的标准是什么呢？在这个追求“现代化”的世界上，只能采纳符合现代化要求的标准：一是看该民族是否具有民主、法治、公平、高效的政治制度、经济制度，二是看该民族是否能创造出现代物质文明，三是看其文化是否具有反省、创新的机能，四是看其社会是否健康、文明、正义。从这四个方面来看，中华民族又确实存在着令人痛心的太多的缺点。<br />
<br />
 <br />
<br />
&nbsp; &nbsp; 中国人几千年深受专制压迫却不知道反省制度，近代以前的历史上甚至未曾出现过一点点民主的理论火花。孔子的德政、孟子的仁政只不过是与专制相辅相成的人治的一种形式而已，他们当然与民主政治是无缘的（一些人将孔孟之道解释为某种民主理念，显然是欺人之谈）。而同时代的古希腊无论是民主理论还是民主制度、民主实践，都创造了早期的辉煌，尽管它们很不完善。中华民族虽然灾难不断出现，两千多年来，内乱、战争大约占了一半的历史时期（有人统计，占了55%），人口死亡过半的动乱就达10次，但没人总结教训、完善制度、提出防范悲剧再度发生的措施，相反，不幸和痛苦很快会被忘却。朝廷也只许歌功颂德，不让揭露问题，并且一味地进行愚民教育，让人们沉湎于皇帝、明君、圣人，陶醉于“太平盛世”，不知道危机迫在眼前、灾难源自何处，于是，愚氓遍地，专制统治得以维持。<br />
<br />
 <br />
<br />
&nbsp; &nbsp; 而且，在漫漫的时间长河中，在悠久的中华“文明”中，最高统治者的权力一直不受任何约束，肆无忌惮，无法无天，可怜老百姓受尽欺压凌辱，即使引颈受戮，亦要感谢皇恩浩荡。还有酷吏、奸臣、宦官、外戚，以及各级暴官、恶官、昏官、贪官也可以玩法律于股掌之上，置人民于血腥之中，不择手段地追逐“富贵”、夺取权力。为此，君臣相杀，父子相残，骨肉相斫，同胞相害，权力斗争将人异化为猪狗不如的野兽。无规则的权力斗争，谎言的说教，腐败的官场，阿谀奉承的官吏，猛于虎的苛政，逆来顺受的百姓，这几乎就是传统中国政治的全部内容。面对这样的政治制度，古代中国人竟然心安理得，两千多年来只管把智慧淋漓尽致地用在饮食男女、求神拜佛、官场钻营上。<br />
<br />
 <br />
<br />
&nbsp; &nbsp; 在经济方面，封建王朝的小农与家庭手工业结合、男耕女织的制度模式千年不变。由于人口不断增加，官府不断膨胀，加上权力不受民众制约的官吏的巧取豪夺，致使历代农民的实际税费负担都很沉重（平均是西方农民负担的5~10倍），农业劳动生产率不断下降，于是老百姓只能在饥荒的边缘上挣扎；而且，封建统治者为了巩固家天下的万世基业，推行重农轻商的政策，以粮为本，只发展单一的粮食生产，为此大规模开荒毁林，导致中华民族的发源地黄河流域由水草丰茂、“水清且涟漪”变为如今的黄土遍地、黄沙漫天。这样落后的经济制度不但不可能创造出丰富的物质财富，相反经常会导致饥荒、动乱，即使这样，也几乎没人对之产生过怀疑，更不用说制度创新。<br />
<br />
 <br />
<br />
&nbsp; &nbsp; 长期以来，中国社会的价值观也发生了严重的扭曲，人们以“泯灭是非心”、“难得糊涂”为座右铭，使得社会正义感沉沦―― 不择手段之举被人赞为“有手腕”，当小人、叛徒被说成“识时务者为俊杰”（所以，抗战时汉奸数百万）。更为严重的是，中华文明从发轫之初就蒙上了专制的阴影，牧民（御民、宰民）思想浓重；同时，不择手段的官场哲学造就了谋略、阴谋文化，形成了一些国人虚伪的道德，窝里斗的习气，阿谀奉承、弄虚作假、野蛮血腥的品行。因此，有人甚至认为中华民族是无罪恶感、无是非感的民族。<br />
<br />
 <br />
<br />
&nbsp; &nbsp; 中华文化也只是感性乐观文化，世俗主义、经验主义盛行（浩如烟海的史籍只是为人处世、官场斗争的经验集成），我们的饮食文化、妻妾文化倒很发达，人们追求大红大绿、福禄寿喜（特别是老百姓对长寿孜孜以求，为此而炼丹、练气功、食疗、药疗），文学作品也以大团圆结束（所以有人说，尽管中国传统社会本身就是一个大悲剧，但却没有真正的悲剧作品）；中华民族也几乎没有严格意义上的宗教（道教、中国禅宗、净土宗等皆具有中国特色而追求长寿、自然等世俗的东西）；我们没有超越世俗的思考，几乎没有形而上的哲学（至今形而上学仍然是贬义词），没有多少高深的理论（古代的原始技术、发明很多，但不能上升为理论，所以，永远不能进步。雨果说：中国是保存胎儿的绝妙的酒精瓶）。我们没有什么逻辑思维――中国禅宗抛弃了印度佛教的因明等逻辑理论；道、禅皆主张直觉；爱因斯坦认为，中国科技的发展没有走逻辑推理、实证实验之路，很多东西只是天才的猜测的结果。中国人最拿手的只是比喻、象征、暗示、零散的思维，阴阳、五行、八卦即是如此；庄子、韩非子则以寓言、故事“论证”其哲学，《论语》、《老子》、禅宗语录等只是格言警句式的思想火花的汇合。<br />
<br />
 <br />
<br />
&nbsp; &nbsp; 从上可见，中华民族在政治、经济、文化，以及制度、行为、思维方式等方面确实存在着重大的弊端，近代以来落后是必然的事。传统中国只能产生专横的统治者、愚昧的民众、贪婪的权贵、无规则的政治、低效率的生产、对不择手段的信仰和社会道德的沉沦，而不可能产生理性、正义、文明。中国人尽管有饮食、计谋等方面的小智慧，但缺乏政治经济制度方面的大智慧。<br />
<br />
 <br />
<br />
看来，我们不得不承认，中华民族在过去确有劣等的方面。现在只有洗心革面，深化改革，重铸国魂，再造制度，中华民族在将来才能创造出现代文明的辉煌。所以，我认为，对中华民族的真正的评判应当在将来。即关键是看我们能否进行制度、文化创新，能否实现现代化。只有实现了现代化，我们才有资格说――炎黄子孙是优等人类！</div>

							
							
							
							
