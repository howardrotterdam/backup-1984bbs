													<h2>一小撮人坐在那里一规划就能把现实改造好？</h2>
						
						
																											<div id="postmessage_331747" class="t_msgfont">罗斯福新政其实不成功，这从大萧条持续到二战爆发才结束（长达10年之久，也有15年之说）就能看出。美国经济学家历史学家很少有认为罗斯福新政是成功的。看看《美国大萧条》（罗斯巴德著）、《美国的历程》（布鲁姆著）等书就知道。<br />
<br />
人们所以支持政府干预，是因为他们认为萧条是市场中人不理性的结果。可是，干预者就一定是理性的吗？他们难道不是人而是神吗？就一定会比市场更少犯错吗？恰恰相反，干预犯错的几率远远大于市场自身。因为，1，市场中的信息是分散掌握在千千万万的个人脑子里的，一小撮政府官员的头脑绝不可能比那些人更聪明地知道应当怎样投资怎样消费。2，政府有任期限制，官员们只追求自己任期内的政绩，不可能像拥有永久产权的私人那样去悉心配置资源。3，基于前述政府干预而重新配置资源会进一步扭曲市场中的各种信号，从而使危机加剧、周期延长。<br />
<br />
理性当然很重要，但不能绝对迷信它。迷信理性就会把人即时的思维和创造能力看得很了不起，以为可以割断历史、无视经验、传统和风俗，一小撮人坐在那里一规划，就能把现实改造好，正如毛贼所谓“一张白纸可写最新最美的文字，可画最新最美的图画”。结果怎样？法国革命、苏俄革命和中国革命的结果无一不是惨不忍睹！<br />
<br />
政府干预就是“一小撮人以为坐在那里一规划，就能把现实改造好”。<br />
<br />
需要明白的一点是，无论是自由市场中，还是政府干预下，危机一定会爆发（因为人不是神，一定会犯错），只是，由前者导致的危机，如果任其自身调节，这个周期就会比较短，调节效果也会比较好（尽管初期的阵痛可能很难受）。如果是后者导致的（多数危机是政府造成的），加上它进一步的干预，调整的周期就会拉长，效果也会越来越糟。<br />
<br />
我隐约觉得主宰世界的那种天力，很可能是千万人对各自掌握的知识的相互利用，构成了人类社会发展的动因。只是这种交互具体是如何发生作用的，目前尚不能完全说清楚。所以哈耶克说理性的作用是有限的。而主张政府干预的人则以为，一小撮人的思考可以代替千万人知识的交互作用，做到万无一失的设计，所以他们乐于幻想一个全新的世界，比如社会主义共产主义。<br />
<br />
理性人是经济学的假设，大概意思是指，人会按照自己利益最大化的原则行事。很可能，多数人多数情况下的确如此。但，正因它是假设，所以并未完全排除人也会非理性行事的可能。而当相当一些人同时非理性（犯错）时，危机就会爆发。当然，“纯市场”中这种可能比较小，即使真的发生了，在没有外力干预的情况下，市场自身也会在较短时间内调整过来。<br />
<br />
自由市场也好，宪政民主也罢，很大程度上都是经验、传统和习惯的产物，是长期摸索出的大致不错的机制，其结果也不过是大致美好而已。除非宗教徒，普通人一旦抱着存在绝对完美的天国的想法，就容易采取极端行动。结果正如荷尔德林所言：“把人间变成地狱的，恰恰是人们试图将其变成天堂。”人的“主观能动性”可以用，但要慎重。<br />
<br />
以上是我在这里的跟帖，汇总在一起，看起来更清晰连贯。<br />
