																											<div id="postmessage_182730" class="t_msgfont">“任志强实话论”与斯德哥尔摩综合症<br />
刘松萝<br />
<br />
斯德哥尔摩综合症，是指被害者对加害者产生感情，甚至帮助加害者的一种情结。关于斯德哥尔摩综合症，百度百科这样介绍：<br />
<br />
1973年8月23日，两名有前科的罪犯Jan Erik Olsson与Clark Olofsson，在意图抢劫瑞典首都斯德哥尔摩市内最大的一家银行失败后，挟持了四位银行职员，在警方与歹徒僵持了130个小时之后，因歹徒放弃而结束。然而这起事件发生后几个月，这四名遭受挟持的银行职员，仍然对绑架他们的人显露出怜悯的情感，他们拒绝在法院指控这些绑匪，甚至还为他们筹措法律辩护的资金，他们都表明并不痛恨歹徒，并表达他们对歹徒非但没有伤害他们却对他们照顾的感激，并对警察采取敌对态度。更甚者，人质中一名女职员Christian竟然还爱上劫匪Olsson，并与他在服刑期间订婚。<br />
<br />
在中国，斯德哥尔摩综合症被赋予特定的政治含义，被用来描述一些人对计划经济和文革时代的留恋。正因为如此，斯德哥尔摩综合症应有的警示意义被降低了。表面上锐意进取，实际上只是跟随潮流的人们会这样说：斯德哥尔摩综合症？那是与专制有关的事情。我们的理论既有自由又有市场经济，哪来的斯德哥尔摩综合症？<br />
<br />
其实，市场经济与自由并不能完全划等号。在市场经济中，变相的奴役和绑架也是存在的，否则就没有“房奴”和“房地产绑架社会”的说法了。<br />
<br />
改革以后，我们从政治决定论转向了经济决定论，以为只要遵从几条经济法则，一切问题都会迎刃而解。对于房价，有人认为只要开放，迟早会像白菜萝卜和彩电冰箱的价格那样能够为大众所承受。但是，住房的供求关系有特殊的地方：第一，土地资源是稀缺的，不可能无节制地占用；第二，大众的收入在增加，而富人的财富增加得更快，这些财富推高了房价。采取放任的政策，房价最终失去了控制。<br />
<br />
现在我们明白一些了，趋势却已经无法逆转。土地财政已经形成，地方政府卖地的欲望无法遏制；房地产商已经，就像一位亲房地产商的网友所说的，已经尾大不掉。阅读那位网友的发言，一种有恃无恐的嚣张跃然纸上。原文是这样的：你知道什么是尾大不掉吗？同时，炒房已经成为一种投资方式，一种文化。因此，任何促使房价下跌的措施都无法实行。<br />
<br />
毫无疑问，整个社会已经被房地产绑架了。同1973年的斯德哥尔摩综绑架案一样，被绑架者没有别的办法，只有看着绑架者的眼色行事。那些曾经用经济学教训公众的人，也只好拿出其他的理由来自欺欺人了。<br />
<br />
当房地产商任志强大放厥词，训斥公众的时候，有不少学人和准学人出来叫好。归附强者，是人的本能。被打败了，被绑架了，大多数人只好去找屈服的理由。任志强说大众找错了对象，哭错了坟头，其实就是在撕大众的脸。没有勇气抵抗的人们转而支持任志强的言论，就会得到一种进入主流，能够和任志强一起训斥别人的感觉。弱者幻想通过追随强者让自己变得强大，这是一种代偿现象。革命胜利以后狂热追随革命的人，追星的人，以及幻想能够与任志强站在一起的人都是这样。<br />
<br />
有人说，任志强说的不过是实话。这种任志强实话论，不过是被绑架者理屈词穷之后的托词。实话不一定是好话，强盗在抢劫的时候说的也是实话。在房价已经高得离谱，民生倍加艰难的时候，任志强的有些实话，比如“承受不了你就别买房子”，已经超出了应有的底线，超出了作为商人的本分。鉴于社会的紧张局面，这种实话只能够进一步激化矛盾。<br />
<br />
鼓吹任志强实话论的学人们，如果你们还有记性的话，就应该想起你们曾经扮演过弥赛亚的角色，告诉大众放任的政策能够解决住房问题。如今你们又换了一副面孔，告诉大众你们是狼，他们是羊。如果还有羞耻之心的话，你们应该休息了。<br />
<br />
一个社会只有一部分人的实话，是很危险的。套用诗人田间的话，假如我们不去说话，我们在辛劳中倒下了，一定会有人指着我们骨头说： “看， 这是奴隶！”<br />
<br />
对于任志强的实话，必须有人用实话来回应。有任志强们的实话，又有大众的实话的社会，才是正常的社会。<br />
<br />
任志强那些充满歧视和挑衅的言辞已经变成了一部分人的公理，激励着他们去挑战公众，羞辱公众。有一句一直想说，又一再忍住不说的话，现在不得不说了：正是有那些藐视公众的人物存在，才有一些并不强大人敢于充当恶狗的角色，面向大众狂吠。<br />
<br />
