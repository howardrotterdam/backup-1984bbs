<br />
而在太空运行的全球卫星定位系统，都是美国人的天下。在美国人已用全球卫星定位系统精确制导导弹的情况下，党卫军的航母能有生存的空间么？<br />
<br />
其六，巨额的航母建造费用，会令中国人民抽干精血，吸尽骨髓。其后果是灾难的，也是不堪设想的。<br />
