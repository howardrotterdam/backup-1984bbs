																											<div id="postmessage_854754" class="t_msgfont">有个网友在独立评论上跟帖转来一篇文章，很有意思，特贴来给大伙看看。<br />
<br />
中国瓦良格航母最新猛料曝光<br />
<br />
来源：中华网博客<br />
<br />
网上到现在还有很多人，说是瓦良格不一定会加入PLA现役，其论点中很重要的证据是，老瓦来中国后，足足在大连港放了三四年，并且谁都可以去看。对于这种观点，可通过我知道的一件事来证明，PLA始终是积极修复老瓦的，之所以放了那么长时间，是因为技术问题，修不了，而不是不去修。<br />
<br />
时间比较久远了，大概在03年吧，那年大连恰好是第一次承办APEC（环太平洋经济合作）组织部长级会议。整个大连市都行动了，尤其是保安更是重中之重。当时老瓦还是在大连港的船坞里停着。当时参加会议的APEC组织各国部长提出要去码头看瓦良格号，大连市公安局和安全局动员了很多警力进行这次参观的安保工作。<br />
<br />
当天晚上，我恰好与我公司经理约好，宴请一名大连公安局一名处长。当时约好的是晚上7点，结果过了8.30此人才匆匆而来。<br />
<br />
吃饭间，他告诉我们，迟到是因为参加了参观活动的安保。这些APEC组织的部长只被允许在码头上看瓦良格，其中有的部长要求上船，但是被国安部门拒绝了。这帮人看了足足2个多小时，才离开。当时我就问他，“瓦良格在哪停着，不修，那不是锈了吗？我们中国真的要把它给拆了吗？”<br />
<br />
他当时说“不是不修，刚拖来时就想修，当时大连造船厂和海军的人，对瓦良格做过检查，发现俄国人的造船水平比国内高的多。<br />
<br />
“其中仅瓦良格用的钢板，就极其先进。我们国内的军舰用钢，也是抗海水腐蚀的，但是性能一般，所以必须要刷防锈漆。好在中国防锈漆做的还可以。但是一般军舰，每三四年就要进船坞进行舰艇底部除附着物，并重新刷防锈漆的大修。否则海水对军舰底部的腐蚀，严重影响舰艇防护能力。<br />
<br />
“而瓦良格用的是俄罗斯的军舰特殊钢。在运到中国之前，已经好多年没有进行过防锈的处理了。但是通过国内检查后发现，瓦良格用的钢，可以自然抵抗海水腐蚀。所以瓦良格外表面状态很好。所以中国拉来后，也一直没有涂装，就在码头那里裸放着。<br />
<br />
“并且，一般重要军舰都是无磁的，也就是可以防止磁性水雷或者通过磁力探测到军舰。但是地球自身是有磁场的，即使用的低磁钢的军舰，在航行几年后，收地球磁场同化，军舰的钢铁也就带上磁力了。所以一般三四年后，军舰都要回船坞进行逆向消磁。而瓦良格用的钢可以说是绝磁的。瓦良格从建造到运回中国，足足将近15年，在到中国重新检测后，磁力依然为零。”<br />
<br />
所以当时他就感叹“老毛子还是厉害，主要人家基础好，苏联留下的家底，中国就是追上20年，也很难全面超于啊，尤其是基础工业水平，严重制约军事设备的发展。”<br />
<br />
他还说“瓦良格，运到大连后，大连造船厂立刻就对其进行了试修复，首先发现的问题是，因为前面提到的俄罗斯的军舰钢是特殊钢，我们国家的钢板比人家差了很多。当我们用我们的钢板进行焊接，发现连接处强度根本不够。而且，由于我国军用钢性能不行，即使修复后，对瓦良格整体防护性能也造成很大的下降。所以，从2000年起，我国就开始与俄罗斯谈判，要引进俄罗斯的军用特殊钢生产技术。”<br />
<br />
“但是俄罗斯要价很高，条件也比较苛刻。光谈判，就谈了很长时间。然后国内还要组织技术进行生产。国内最先进的特殊钢生产基地是抚顺特钢公司（是日本人1937年建的），中国的核潜艇，火箭，沈飞的飞机发动机用的钢，基本都是抚顺特钢造的。但当时抚顺特钢因为国企改革，工人下岗的很多，除了几个军品生产车间，几乎是出于半停产状态，并且据说还要卖给一个民营的钢厂。但因为这种特种钢只有让抚顺特钢生产把握最大，所以必须先解决企业的问题。<br />
<br />
“当时，薄熙来是从大连市市长刚升到辽宁省省长，他牵头由效益较好的大连钢厂联合抚顺特钢组建了东北特钢集团（后来还上市了），由大连钢厂出钱，抚顺特钢出技术，在大连建了一个生产这种俄罗斯挺军舰钢的生产基地。据说化了十几个亿，并且还要每年给老毛子交很多专利费。”<br />
<br />
“通过这么多事，才初步解决了进行修复瓦良格的第一步工作，能生产瓦良格用的钢。”<br />
<br />
当天那顿饭主要讨论一些别的事，关于瓦良格的故事，也就谈到那里为止了。<br />
<br />
单从这里可以看到，我们PLA不是不想修，不是不想要航母，而真是是落后啊，东西都买来了，修都修不了，就为了一个军舰钢的问题，足足就折腾了三四年。瓦良格会加入海军服役是确定无疑的，要不也不用费这么大劲，投入这么多经费和资源了。<br />
<br />
无论是训练舰还是正式的战斗舰，都是我们中国人的大喜讯啊。但这些年我就一直在想，我们军迷，一直喊着爱国，扩军，有人还埋怨我们国家军费够，领导人胆子小。但实际情况呢，首先要实事求是的面对的事，我们国家相对还是比较落后的工业基础。这个天生不足的条件，不是拿钱就能立刻解决的。就是用钱去砸，也是需要时间的。何况除了老毛子和以色列，别的国家用钱砸人家，都不给你这个机会。<br />
打铁还需自身硬，以前我们穷。我小时候，我们讲努力工作，为祖国发展做贡献。现在我们不讲这个了。但我觉得，我们依然要努力去工作，尤其是在国企的同志们，国家的强大真的不是用钱来衡量的。<br />
<br />
要我们一代一代人的积累。我们军迷不但要在论坛上呼吁支持国家，更要体现在我们实实在在的工作上，尤其那些从事高技术含量工作的兄弟们，你们一点一滴的积累才是我们国家军事实力增强的基础啊。<br />
<br />
也不要太埋怨国家胆子小，不造航母。航母几万吨，不比几千吨的驱逐舰，三四年就大修一次。美国航母一次进船坞大修，要间隔十多年，一次进坞大修要历时半年甚至一年。<br />
<br />
以我们国家原来的技术，造出来的航母，仅消磁和防腐，就要求必须三四年大修一次。这样航母的战斗值班时间就大大缩短的。并也花费巨大，性能也受影响。<br />
<br />
所以，过去国家始终在造与不造航母之间犹豫，还是可以理解的。现在技术有了，不是马上就启动造航母的方案了吗？</div>

							
							
							
							
