													<h2>中国造航母有个屁用</h2>
						
						
																											<div id="postmessage_853449" class="t_msgfont">荆楚<br />
<br />
前几天，有网友专门著文论证：中国要打败美国，需要造多少航母？<br />
<br />
惹得一群人在那个帖子下面，血脉喷张，摩拳擦掌，跃跃欲试。<br />
<br />
我看了那个帖子后，于是也在下面跟帖说：中国不需要造多少航母，只需要造一艘“航公”就成啦。美国那些航母，一看到中国的航公，就撅起屁股，哇呀呀大叫起来啦。<br />
<br />
于是遭到网友的批评，说我老不正经，把这么一个严肃的话题，搞得这么“恶搞低俗”云云。<br />
<br />
好吧，我就接受网友的批评，正儿八经来说说这个问题吧。<br />
<br />
一、中国的出洋通道在哪里？<br />
<br />
回顾世界海军发展的历史，英国皇家海军曾一枝独大。到今日，则是美国海军首屈一指。而美国海军是在马汉的海权理论的指导下，在老罗斯福总统的大力推动下，才发展壮大起来的。当然，马汉的海权理论，是在英国皇家海军辉煌业绩的刺激下，才归纳总结出来的。<br />
<br />
後来，在现代航空作战的刺激和制约下，才开始谋划和建造航母。并逐步更新，逐代完善进步。到今天，航母已是远洋作战利器。它体现了一国的技术成就，也体现了一国的军力和国威。它宣国威于远洋全球……<br />
<br />
有个笑话调侃美国：一旦世界上哪里有事，美国的伯里玺坦特（中国人按照专制文化的思维惯性，把“President”翻译成“总统”，实在是想当然的极大错误。所以我实在不想用这个充满专制毒素的中文词汇）就问：我们的航母在哪里？<br />
<br />
故此，惹得中国的“现代义和拳民”整天大呼小叫起来――我们为什么不造航母？<br />
<br />
针对这些无知的叫喊，我只好放点耐心，先从地理上来谈谈这个问题，也算给这些“现代义和拳民”补补地理课吧。<br />
<br />
中国在满清统治者的颟顸无知而又异常严酷的思想奴役统治下，在闭关锁国政策制约下，国疲民弱发展到极致了，国人仍然沉迷于“天朝上国、唯我独尊”的状态中，不愿醒来……到後来，不得不放弃对安南、缅甸、朝鲜的主权。<br />
<br />
二战结束后，国民政府争取到的，在1954年，将海参崴和江东六十四屯归还中国的外交成果，也被毛刘周等卖国贼，为了讨好其外国爷爷，而拱手送给了斯大林……以致到今天，中国只剩下东边的海岸线。<br />
<br />
而要从东边的海岸线航行到外洋，只有三个出洋通道。<br />
<br />
其一是韩国与日本之间的对马海峡。这个海峡，被对发动韩战制造灾难的中共怀有深深恐惧的韩国和日本掌控，被美国势力深深渗透，更被美国的关岛锁住咽喉，岂容中国航母安全通过？<br />
<br />
其二是经台湾海峡到南海，再到马六甲海峡，再出外洋的通道。这条通道比对马海峡更为严峻和凶险。估计中国航母开到半途，就歇菜了，就死翘翘了。<br />
<br />
其三是经琉球群岛与台湾之间的海域，再出到远洋。这条航线满布珊瑚礁，为历代航海者视为畏途，视为探险航程。而动辄数万吨的现代航母，其吃水深度远甚于商船和民船。航母能否从这条航线上出到外洋，都成了问题。何况日本、台湾、菲律宾等国，对中共怀有深深敌意，岂不虎视眈眈？航母从这里出洋，难于九天摘月。<br />
<br />
不能出洋的“远洋作战利器”，只能关在近海“过家家”，有个屁用。<br />
<br />
它只让国人背上一个沉重的财政抱负而已。<br />
<br />
二、周边国家的国际环境<br />
<br />
首先，中共建政后，曾不惜国内人民饥寒交迫，大力向周边各国输出革命。其结果是，给这些国家和人民造成无穷的苦难和血泪。从而造成了周边各国对中共的深深敌意。<br />
<br />
印尼、越南多次驱赶华侨，就是这些“输出灾难”的后遗症。更有中共派出的众多“军事专家”，直接帮助波尔布特大肆杀害华侨……<br />
<br />
中共在周边国家恶贯满盈，却要我苦难不幸的华人华侨来买单。<br />
<br />
而在华侨受到惨绝人寰的迫害时，全世界都为之落泪。唯有中共卖国政权，以“不干涉内政”为借口，做了缩头乌龟。<br />
<br />
事后还将大把的美元，输给这些丧心病狂迫害华侨的国家……世界上再也找不出华侨这种血泪连连的族裔了。<br />
<br />
其次，中共与整个世界文明为敌，与各国人民为敌。却厚颜无耻地指责文明国家“敌视‘中国’”。动不动就使出“我是流氓我怕谁”的下三滥招数，拉拢几个国际上的流氓政权，拉拢几个独夫民贼，与文明世界唱对台戏，向人类文明叫板。这样的流氓政权，能赢得周边国家的尊重么？<br />
<br />
其三，中共一惯穷兵黩武，不惜国内数千万人民饿毙沟壑，也要大量出口粮食，换来设备和技术，从而爆炸了由数千万饿殍冤魂凝聚起来的蘑菇云。但随着蘑菇云的升起，不但没有赢得周边国家和文明世界的敬重，反而遭到周边各国的一致鄙视和厌恶，从而而与周边各国的关系进一步恶化。<br />
<br />
在这样的国际环境下，中共的航母，必然成为“过街老鼠，人人喊打”。哪有远洋生存的空间？<br />
<br />
三、中共党卫军现状评估<br />
<br />
首先，拿破仑曾经说过，军队的战斗力，80％来源于士气。而士气的80％，则来源于信仰。一支没有信仰维系的军队，必然是一盘散沙，必然是一具行尸走肉，必然是一支兽兵部队。<br />
<br />
当今的中共党卫军，哪来什么信仰和士气。让他们为一党专政效劳？可是这个党却是一个异常专制腐败嗜血冷酷的魔鬼。让他们为国家民族效劳？可是“党指挥枪”，“一切行动听党指挥”，他们只能为一党之私奔波操劳。虽然他们由全国纳税人养着。<br />
<br />
于是，他们只信奉权钱二字。他们只会利用手上的特权，大搞腐败。他们只会使用手上的坦克机枪，对手无寸铁的国内人民穷凶极恶。而一旦遇到外敌，他们立即尿湿了裤裆。<br />
<br />
中共党卫军的腐败，被重重黑幕包裹着。我也不能在此多说什么。只说一件小事，就可以说明问题了――听一位复员回来的士兵讲：他们连的士兵，争着帮连长和指导员洗内裤，士兵们竟争得打起架来。<br />
<br />
这样一支没有灵魂的军队，这样一支腐败透顶的部队，哪来什么战斗力？<br />
<br />
今天的党卫军部队，比历史上的绿营还要糟糕，比北洋海军更为孺弱低能。他们就像鸦片战争时期那样，英国开来2700多名海军陆战队士兵，就可以把40万绿营兵打得落花流水。<br />
<br />
其次，今天的中共党卫军部队，现在只能关起门来洗脑，只能灌输给他们“党指挥枪”的歪理邪说，只能在“军人以服从命令为天职”的精神控制下，训练他们的盲从精神。而不敢让他们接触任何人类文明的东西，隔绝于人类文明和科学系统之外。<br />
<br />
他们一旦与人类文明的价值观有所接触，这些党卫军官兵，立即在精神上，在思想上土崩瓦解，未战先溃。<br />
<br />
这样一支没有人类文明积累的军队，这样一支愚昧无知的军队，哪能掌握现代航母系统的协调、操纵、控制，哪能形成作战能力。<br />
<br />
再次，现代战争条件下，飞机在航母上起降过程中，其自身的炮火要严格管制，以免误伤自己。这时候，是航母最脆弱的时刻。为了克服这个矛盾，所以需要组成航母集群，以便相互支援和相互提供保护。<br />
<br />
既然需要至少两艘以上航母组成航母集群。为了保持常备“战力”，那就不能仅有两艘航母，就万事大吉。而是需要航母的“预备队”。所以一旦启动建造航母，就不是建造一两艘就煞得住的。<br />
<br />
其四，在一个贪污腐败、贿赂公行、国将不国的时代，启动航母建造，也就启动了巨大的贪污腐败机会。建造出来的航母，很可能还没有开仗，就散架了。<br />
<br />
其五，远洋作战，对信息系统的依赖，远甚于近海作战。否则只是聋子和瞎子，只有挨打的机会，而无法制敌。<br />
<br />
