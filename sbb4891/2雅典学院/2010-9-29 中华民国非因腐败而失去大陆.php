难得看到一篇颇有见地的文章，助人透过共党的洗脑宣传而领悟历史的真相。我把标题略改一下，更为准确。作者名字待找到之後补加。<br />
<br />
南京国民政府非因腐败而失败<br />
<br />
不幸的是，刚刚赢得了一场伟大民族战争胜利的中华民国，及其执政党中国国民党，转眼就迎来了一场更加血腥、更加残酷的内战──这就是中华民国的第二大历史阶段s一九四五至一九四九年的内战时期。显然，这是中华民国最为悲惨的一个历史时期，因为她在这一场内战中遭遇了惨败；这又是後来的蒋介石和中国国民党万口莫辩的时期，因为“墙倒众人推”的人间逻辑，又使得人人都只想在失败者那里寻找他们活该失败的原因。因为诸如“成者王、败者寇”和“胜利者才是正确的”这样一些“名论名言”，已经左右了几千年的人心。犹如斯大林後来表扬中共领袖刘少奇所说的那样s“你们是胜利者，你们就是正确的。”<br />
<br />
然而，胜利者不一定就是正确的。成功者未必是“王”，失败者未必就是“寇”。就算成功者是“王”，也不见得就是一个好“王”，也完全可能是一个坏“王”，或是一个真正的暴君。就象三个匪徒劫持了一架飞机，我们能说，成功的劫匪便一定是正确的，而被劫持的数百位乘客就一定是错误的吗？再如，一个文弱书生被几个小流氓在大街上将他打翻在地，不独满脸是血，而且遍体是伤，还被踏上了几只脚，於是我们便一定要认为，只有那几个胜利了的小流氓才是正确的，而那个书生便一定是错误的吗？我们就不说列宁、斯大林直至所有共产国家的大大小小的专制君主们了，他们又有哪一位不是用阴谋和杀戮来为自己铺平权力道路的呢？他们又有哪一位不是错误的、甚至是极端罪恶的呢？<br />
<br />
因此，如果我们愿意从最基本的历史事实出发，来重新研究和评判一下一九四五至一九四九年的中华民国，我们立即就会发现，中华民国恰恰是一个正确的失败者，而不是一个活该失败的不正确者。这是因为s<br />
<br />
第一，诚如我在上面所说的那样，一九四五年的中华民国南京政权是一个拥有合法权力的政权。<br />
<br />
第二、一九四五年至一九四九年的中华民国及其政权，又是一个刚刚赢得了外战、就立即开始迅速推进民主宪政的国家和政权。历史的事实是s抗战期间，国民党和国民政府不仅在实际上已经开放了党禁，结束了训政，而且已经数度征求国民的意见，修改了一九三五年草拟的“中华民国宪法草案”，并为了在战後迅速推行宪政，在战争期间发展民主政治，竟在战争术状态下相继成立了国民参政会、宪政期成会和宪政协进会等志在推展民主宪政的机构。历史的事实是，抗战尚未结束，国民政府就已经宣布要在战後一年召开“制宪国民大会”，抗战刚刚胜利，国民政府就立即邀请已经发动了内战的中共，进行了“双十会谈”，签订了“双十协定”，召开了真正的“政协会议”，通过了民主建国的各项决议。一年後，又践诺召开了“制宪国民大会”。并为了能够邀请中共参加会议，三次推延会期。特别是通过了一部真正民主的“中华民国宪法”。这部宪法，尤将“政治民主化、军队国家化、各党派平等合法化和各级考试和监察官员必需超越各党派”的条文，正式地写进了这部宪法，从而将自“中华民国临时约法”诞生以来，历经三十余年坎坷历程才终於制定的“中华民国宪法”，真正具有了根本的民主性质。诚如胡适之先生所言s“这是一部比美国宪法还要民主的宪法”。虽然，战後迅速推进的民主宪政进程，又被迅速地毁灭在中共所一心要点燃的战火之中，但是，一九四五至一九四九年的中华民国，正因为是一个曾奋力抗战救民族的国家，还因为她是一个在战後立即开始推进民主宪政的国家，所以，她才是一个正确的失败者，而不是一个活该失败的不正确者。此其一。<br />
<br />
其二，虽然中华民国不是一个活该失败的正确者，但她还是遭遇了凄惨的失败。那麽，失败的原因究竟是什麽呢？<br />
<br />
我以为，中华民国在内战中失败的原因有这样根本的四条s第一是日本帝国的长期、并且是疯狂的侵略。这一场战争，不仅将中华民国的国底打空了，而且将中华民国打得遍体是伤，满身是血，也就是说，打赢了战争的中华民国，自己实际上也已经打垮了。特别是由於中共在抗战时期执行了不抗日的阴谋扩张策略，胜利了的中华民国已经不再是战前那个已经获得了基本统一的国家。战後的中华民国，已经是支离破碎、肢体不全，国家和政权早已出现了分裂的悲剧局面。何况，战争不论是对於战胜国，还是对於战败国，都只能造成“创伤”，而不能造成“创强”。就象大清朝摄政王多尔衮对降清的明朝大臣们所说的那样，“大清绝不是得自於大明，而是得自於闯贼”。因此，我们也可以说s“共产党的天下绝不是得自於国民党，而是得自於日本的侵华战争。”因为，最有力的证词，莫过於毛泽东曾感激涕零地对他的日本客人左左木更三所说的话了。他说s“应该给你们发一个大大的奖章，因为没有你们，就没有我们！”至於周恩来所言s“如果没有日本的侵略，中国的革命至少要推迟五十年才能成功。”他所说的革命，自然是他们的共产革命；而他所说的五十年後，则已经是他们的工人阶级祖国──苏联崩溃的时候了，更是国际共产主义运动的血腥铁幕已经被彻底揭穿的时候了。如是，中国的共产革命，还有成功的那一天吗？<br />
<br />
中华民国在内战中失败的第二个原因是“苏联长达二十八年的蓄意颠覆”。这是胡适之先生的话。这句话道出了一个真理，就是我们的祖国不仅有一个恶邻日本曾公然地侵略了我们，而且她还有另一个恶邻苏俄，它不仅公然地侵略过我们，而且在长达二十八年的时间里面，命令并直接指挥中国共产党武装颠覆中华民国，不达目的绝不甘休。我将在讲演的後一部分，详细地说明这个至关重要的问题。<br />
<br />
中华民国在内战中失败的第三个原因，则是自辛亥之後直至一九四九年，因外患连连，内乱频仍，而造成土地和农民问题未能解决，从而为中共在西方共产主义革命这个诱人的旗号下，发动一场传统型的现代农民造反，造就了相当的社会基础。因为早在一九二六年一月，斯大林就在他给中共的指示信中说过s“中国共产党领导的革命就是农民革命，中国共产党成就的大小，就要看中共能够掌握多大的农民武装力量。”而毛泽东在抗战期间就更是一再地告诉他的党人说s“中国共产党领导的革命就是农民革命，中国共产党领导的革命战争就是农民战争。”<br />
<br />
中华民国在内战中失败的第四个原因则是，中华民国虽曾获得过名义上和形式上的统一，或者说是初步的统一，却没有完全获得实质上和根本上的统一。或者说，北伐之後开始逐步走向统一的中华民国，虽然逐一地战胜了残余军阀的武装叛乱和中共的武装叛国，但是，正是後来那一场旷日持久的卫国战争，竟使她的统一进程遭到了夭折。在中国大陆河南人民出版社出版的《国民党起义将领》一书中，所记述的十九位着名的国民党叛将，就有十六人出身於朝附夕叛的封建地方军阀，而非真正的国民革命军；其中有一人出身黄埔，但却是长期埋伏在黄埔和国民革命军中的中共地下党，他就是策动川军叛乱的郭汝槐；另一人则是北伐时期曾与中共暗合的左派军人程潜。<br />
<br />
我承认，中华民国在内战中的失败，当然还有更多的原因可以追寻，特别是中国国民党和蒋介石先生所曾有过的严重错失。但是，反思一九四五和一九四九年的内战，上述四条，应是她失败的根本原因。<br />
<br />
其三，我知道，当我叙述了中华民国在内战中失败的主要原因，却未提及国民党的腐败，一定会令人感到，讲演者有袒护国民党之嫌。因为，在一九四九年之後，几乎全世界的历史学家都因“墙倒众人推”这一句老话，而指责“腐败才是国民党失败的主要原因”，甚至是唯一的原因。至於胜利者中共，就更是要以腐败和反动，来给国民党的失败定罪了。虽然，一九四五至一九四九年的国民党确曾腐败过，甚至在内战和走向战败的过程中，迅疾地变成了一个腐败的政权，但是，我仍然要坚持的是，国民党却不是因为腐败而失败。我的理由如下s<br />
<br />
第一，就象革命一词，不能概括所有的革命一样；腐败一词，也不能包揽所有的腐败。我姑且不说，今日中共的腐败早已越过了当年国民党腐败的百倍、千倍和万倍，我单单要问的是，为什麽连中共都承认，从一九四五年抗战胜利、内战开始之间才迅速腐败起来的国民党，奈何只腐败了短短的四、五年，就失败了？相反，当今那个早已经偷偷腐败了几十年的中共上层统治集团，和现今也已经公然和疯狂地腐败了至少二十年的中共层层统治集团，却为什麽至今不败？甚至还要被海外的某些华裔学者捧上了天呢？直至说中共永远也不会垮台呢？其根本原因，就是国民党政权是被共产党已经足足准备了八年的内战打垮的，就不说苏联的蓄意颠覆和全力支持了。所以，只要设想一下，如果在今日的中国大陆，已经有一支旨在打天下的造反大军，正在为夺权席卷万里关山，只怕中共及其政权早已成为“史话了”了。据此，我才敢说，腐败不是中华民国南京政权在内战中失败的必然原因、根本原因和唯一原因。<br />
<br />
第二，同为腐败，中共是制度性腐败，而国民党及其政权在战後和内战中才迅速发展起来的腐败，却为战争本身所造成。因为，抗战的军费，至一九四四年，就已经高达百分之八十以上，就经济而言，这个国家就象是灭亡了一样。由此而形成的，战时经济的高集中，特别是通货的急剧膨胀，又造成了物价的飞涨，中产阶级的消亡，和人民生活的愈加艰难，也因而才造成了“军人如不吃空军飨，须有超人的克制；文官如不贪污，则需要相当的人格”这样一种极其不利於国家的局面。然而，这绝不是中华民国在战後所独有的腐败现象。可以说，战後所有曾参战的国家，不论是打人的，还是被人打的，几乎都出现了因通货膨胀而出现的不同程度的腐败现象。然而，只因为别的国家都没有立即迎来内战的爆发，所以由战争而引发的腐败，才会因经济的复苏和战争创伤的修复，而走上了正常的道路。然而，外仗刚结的中华民国，却在刚刚胜利的那一天夜晚，就迎来了一场更加残酷的内战。中华民国依靠自己推进民主宪政和发展自由经济来修补战争创伤、消弭通货膨胀和根治腐败的愿望，亦终於随着这场内战的疯狂爆发和痛苦发展，而成为泡影了。<br />
<br />
第三，古往今来，一个政权的腐败，固有其不同的原因，但是，一个正在战争中、特别是在内战中走向失败的政权，腐败则无疑是它“忠实的伴侣”。这个伴侣，还会因为失败的加速，而加速地腐败起来。因为，战争只能使得通货更加地膨胀，人心更加地绝望，社会更加地混乱。今日中共那些仅仅还处在担心“亡党亡国”的大小官僚们，在八九年後就纷纷敛财留後路，送子女出国，转移钱财到海外和在国内及时行乐的种种行径，尚且已经造成了腐败的疯狂加剧，那麽，一九四五至一九四九年正在内战中迅速走向失败的中国国民党及其政权，所出现的腐败加剧的现象，难道不正是由那一场内战本身所造成的吗？<br />
<br />
第四，诚如一位中国大陆留学生在网上攥文所言，近几十年来，共产党有的是时间吃喝嫖赌搞腐败，国民党则根本没有时间游山玩水搞腐败。不说自一九二七至一九四九，前後二十二年，外患内忧交相煎迫，甚至无一年、一月没有枪声。单单只说一九四五至一九四九年的内战期间，只因为外仗刚结，内战已起，前方的战场上正打得血肉模糊，而後方又到处是一片被中共挑动起来的“混乱和暴乱”。彼时，国民党的党政军大小官僚们，其中的很大部分，仓皇维持尚嫌无力，又怎有大量的精力去腐败它一番又一番呢？这位大陆留学生的高论，是否也可以作为国民党非因腐败而失败的一条理由呢？<br />
<br />
当然，我们绝不可以为一个腐败的国家、政权和政党辩护。因为，由任何原因而引起的腐败，都是人民所不能允许的。但是，在探讨中华民国於内战中失败的原因时，我们却应该据实而论。这样，才能使我们真正能够认识到中华民国在内战中失败的根由。而这个根由，就是曾赢得了进步，并曾战胜了侵略的中华民国大陆政权，是被日本帝国、前苏俄共产帝国和中国共产党联合起来共同打败的。这才是历史的真相所在。也才是历史的应有回答。<br />
