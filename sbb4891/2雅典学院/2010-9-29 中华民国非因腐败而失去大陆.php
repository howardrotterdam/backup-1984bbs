<br />
难得看到一篇颇有见地的文章，助人透过共党的洗脑宣传而领悟历史的真相。我把标题略改一下，更为准确。作者名字待找到之後补加。南京国民政府非因腐败而失败不幸的是，刚刚赢得了一场伟大民族战争胜利的中华民国，及其执政党中国国民党，转眼就迎来了一场更加血腥、更加残酷的内战──这就是中华民国的第二大历史阶段s一九四五至一九四九年的内战时期。显然，这是中华民国最为悲惨的一…<br />
