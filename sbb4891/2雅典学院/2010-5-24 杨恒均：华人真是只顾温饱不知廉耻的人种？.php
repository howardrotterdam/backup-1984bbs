													<h2>杨恒均：华人真是只顾温饱不知廉耻的人种？</h2>
						
						
																											<div id="postmessage_635129" class="t_msgfont">杨恒均：华人真是只顾温饱不知廉耻的人种？<br />
<br />
唐人街发生的一件小事，至今让我羞愧不已<br />
中国23岁女留学生在纽约被一疑似醉酒的暴徒袭击、强奸和残杀，事发繁华的法拉盛唐人街附近，事后公布的录像带显示有好几位华人眼睁睁看着暴徒把中国女子拖进小巷施暴，没有阻拦，没有报警，甚至没有帮忙呼救，只是冷漠地转身离开了……<br />
　<br />
即便警方没有公布录像带，我也相信，如果当时确实有好几位目击证人，却无动于衷的话，一定是中国人，而且，我甚至可以更武断地下个判断，那些冷漠的“中国人”一定是第一代新移民，也就是说移民或者偷渡过去不久的中国大陆人。<br />
　<br />
这件事让我想起三年前在悉尼唐人街遇到的一件小事。当天我约了一位悉尼的作家在唐人街购物中心（Market City）的快餐厅见面。快餐厅 （Food Court）中午人较多，我提前赶到占了一个靠窗的座位。大概快到中午12点的时候，整个快餐厅的一百多个坐位几乎都被占了，朋友还没有来，我在喝奶茶看书……　 <br />
<br />
突然听到一声惊呼，伴随着椅子擦地与摔倒的声音，我随着惊呼声与大家的目光看过去，发现一位老人家从椅子上滑到地上，旁边的老伴惊呼出声。我站了起来，朝事发地点走了几步，想看清楚是怎么回事。这时，整个餐厅一百多号人的注意力也都被紧紧被吸引了过去，除了零星的跑步声与询问声，一片寂静，很多食客送到一半的食物停在半空……就在这短短十几秒的时间里，一位冲上去的年轻人已经蹲在了老人家面前，好像在询问病情，以及是否需要叫救护车。另外一位跑过去的帮忙挪开了椅子，随即，那老人家好像没事一样，在第三位赶过来的青年人的帮助下，缓缓站了起来……　 <br />
<br />
站起来后的老人家重新坐好，向旁边食客歉意地点了点头，用中文说了几句话。我听不清他讲什么，但从坐在他附近人听到他的话后，脸上的表情立即放松来判断，老人家没事了。随即大家都很礼貌地移开了目光，停留在空中的食物继续它们的旅程，纷纷落于众人的嘴巴中……这位中国老人家显然只是跌倒了，好在没有跌伤。我也松了一口气，准备坐下来继续悠闲地喝我的咖啡、看我的书――然而，就在我坐下前又扫了一眼大家不再注意的“现场”时，我一下子愣住了，随后，那整个中午，甚至在后来三年的时间里，只要我一想到当时的“现场”，我几乎都会立即回到“坐立不安”的反思中……　 <br />
<br />
这是悉尼唐人街上最大的中餐快餐厅，当天中午这个餐厅里已经坐满了人，而这些人中至少超过五分之四是华人，特别是年轻人，只有不到五分之一的其他人种，例如白种人或其他亚裔人。当那个老人跌倒的一瞬间，就在我转头寻找目标，站起来走近几步，想搞清楚那老人摔倒后是否会爬起来的时候，已经有至少六位年轻人从各自的座位上跳起来，第一时间冲了过去……其中三个就是我说的冲到老人身边询问他，并帮他站起来的。还有三位跑了几步，看到没事，就停了下来。　 <br />
<br />
这种情况当然是经历了不到一分多钟的事件结束后我才观察到的，因为，当大家都松了一口气，继续忙不迭地把食物送进嘴里的时候，那几位冲过去的年轻人开始缓缓走回自己的座位，这也就是我准备坐下时突然注意到了他们，注意，他们并不是坐得离老人家最近的，有一位甚至在我后面的座位，但他们之间有一个特征，那也是至今让我想起来还深感羞愧的特征――　 <br />
<br />
<strong>除了一位之外，其他冲过去或者正在跑过去施救的五位都是年轻的白人！注意：当时坐在这个餐厅里的一百多人中只有不到二十位“外国人”（也就是我们常说的白人），其他的都是黄皮肤黑眼睛的同胞(当然，很多已经是澳洲国籍的“外国人”)。而坐在那位老人附近的，几乎都是华人。更要命的是，当那位唯一一位冲过去施救的华人（从他和几位华人坐在一起判断）经过我座位回到旁边一条桌子上的时候，我从他几乎不带任何口音的英语判断，他要就是第二代移民，要就是很小就来到了澳大利亚……在这个餐厅吃饭的中国人，绝大部分是讲普通话的……</strong>　 <br />
<br />
中国老人从坐凳子时摔倒到爬起来，前后也就两分钟左右，我相信当时没有多少人像我一样注意到这两分钟从四面八方冲过去的人是什么种族的。可这件事对我的冲击之大，至今让我想起来都依然会脸红。<br />
　<br />
也许我的读者会问，你为什么一直没有写出来？原因是复杂的，最主要的原因就是我很少写我想不清楚的事件，更不愿意用没有答案的问题折磨我的读者。这件事如果如实写出来，如果我写出来时还没有自己的结论和观点的话，我的读者大多会得出“中国人（甚至中国这个种族）真他M的冷漠”的结论，这对于始终对中国人怀抱希望的我来说，是雪上加霜的羞辱。也许，那位唯一冲过去的华人给我们另外一个思考？让我们把结论换成：“凡是在大陆受过教育的人都如何如何”。同样是中国人，换一种教育，换几本课本，教育出来的是不同的中国人……也许，更大的原因是：就在我为满餐厅华人都不冲过去而感到震惊的时候，我不能否认的事实是：我自己当时根本没有想到要“冲过去”，我虽然也站起来了，但我站起来是为了看得更清楚，我从头到尾没有想到要走过去帮忙……<br />
　<br />
我当然可以为自己找到理由：我当时并不常住在悉尼，来到这里，我属于不习惯环境的外国人；我坐得比较远，加上我不懂得急救知识（这个很重要，注意，前面提到的外国人上去后不是马上去扶老人，而是先询问情况，因为有些突发疾病不能立即扶起来。这显示第一个冲上去的外国青年懂得急救知识），而据我所知，当地澳洲学生，很多人都业余学过人工深呼吸等急救知识；还有一个理由，当时这个餐厅里几乎都是年轻人，特别是青年学生，坐在那两个老人周围的就有很多，我有点依赖他们……<br />
　<br />
当然，上面这些理由一条也占不住脚，那天的事实是，在一个中国人云集的地方，一位中国老人跌倒了，而冲上去施救的竟然是少有的几位白人青年。如果说三年前悉尼唐人街那间挤满华人的快餐厅带给我的是震惊与失望，那位同五位白人青年一起冲过去施救的华人小伙子毕竟还给我留下了一些希望。当然，不让我绝望的，还有我自己三年来一刻也没有消除的内心深处的深深羞愧。<br />
　<br />
也许，当大多中国人看到纽约唐人街的新闻时还能够感到愤怒、悲哀与羞愧的时候，说明我们的民族依然还有希望……<br />
　<br />
就在纽约唐人街发生那个悲剧的时候，悉尼唐人街迎来了好消息：鉴于华人把唐人街弄得热闹非凡，美味的中国食物吸引了络绎不绝的当地与世界各国食客，悉尼市政府决定，扩建唐人街，把附近的小巷子都弄成美食一条街……可以预见，中国人还会络绎不绝地涌向世界各地，开辟一条又一条的唐人街，向世界输出我们美味的食品，但我们什么时候才能向世人展示我们不是只顾温饱而不知廉耻的人种，也不是没有价值理念、甚至失去了道德底线的民族……？！<br />
　<br />
杨恒均 2010/5/23</div>

							
							
							
							
