													<h2>江胡斗 看天地翻覆</h2>
						
						
																											<div id="postmessage_380360" class="t_msgfont">【 阿波罗新闻网2009-12-17讯】&nbsp; &nbsp;<br />
放屁、催奶与便秘 <br />
─北京官场人事 <br />
（大陆） <br />
刘仲温 <br />
<br />
「有时候已经很努力了，结果却是个屁」，这话是网上流传的某种所谓猫言猫语。不过，用来说那些毛子毛孙们治理中国的成绩也差不多。从十月一那天到现在，我老人家在京城观察政局的感想，大抵如此。也是因为在党内工作的时间长了，对那些升官图也算熟悉，说实话，一连两个月，我老人家都很惦念他们争权夺利中的得失。从十月一到十一月底，北京的官场这个叫憋气呀，谁都连尾巴稍儿不掀一掀，净顾上收拾老百姓了。终于，到了十一月的最后一天，憋的这股子气，放出来了。不大响，也不大臭，磨里磨叽、窝窝囊囊的那么一个蔫儿屁，典型的胡锦涛风格。不用说，那就是几个省区的书记人事调整。本来，一个屁不值得写篇文章。但是，一则，中国据说是太平盛世，除了放屁打嗝似乎别无大事，或者有大事但是我们小老百姓无法知道，所以只好写写这个。二则，对高明的中医医家来说，从屁也可测知身体的状况。我老人家医道很不高明，但不妨试试，抛砖引玉也。于是乎，今儿我就从这个屁说起，把把中共的脉象。 <br />
<br />
　　江泽民「经营十八大」 <br />
<br />
十月一那天我老人家窝在胡同里，很不想看电视，不过实在没有什么别的好看。结果，就看了。一看就看到了毛主席他死老人家的画像，还有「毛泽东思想万岁」的大标语。 「万岁」个屁！毛老死的被人喊了多少「万岁」，到了年岁照样翘辫子。然后就是胡锦涛那个衰样，三分之一像老毛，三分之一像李鹏，还有三分之一像金正日，居然也抬出一幅画像来。当年初进政治局常委的时候，得说人家锦涛同志还算得上个帅哥。将近二十年这么个日日夜夜阴谋诡计弄下来，弄得胡破了相，八字眉、三角眼不说，嘴还是歪的，一说话一扯咧，帅哥变成了衰哥。再配上那身整个从死人身上扒下来的毛式送老衣裳，真是把庆典办成了彩排的丧礼。 <br />
<br />
当然，最糟心的，还是看到了「第三代核心」在胡锦涛身边鬼魅一般的如影随形。头发也不染了，黄??的，乍看倒像个时髦粪青，不过就是太稀绺了。一般认为，这说明江泽民仍然保持着巨大的政治影响力。可是，在我老人家看来，事情都有另外一面。如果他的影响力真的那么大，还用得着拼上老命自我在天安门当众罚站一天一夜？老毛那个时候，躺在床上，喘气都不顺当了，周大管家还要在他耳边大声效忠：「主席，大权都在您手里！」那才叫有政治影响力。今天这个，应该叫做「争取政治影响力」。不错，有影响力也是争取来的，一天不争取一天玩完。 <br />
<br />
听人家说，江前总书记早就放出风来，要「经营十八大」──听着像要做一笔生意似的吧？生意场上，你要经营，人家也要经营，钱最后让谁赚到，这不是一厢情愿能够决定的。果然，就在十月一之前的十七届四中全会上，江失利了，未能如愿把他本人和曾庆红同志一起所选定的隔代接班人习近平同志推到军委副主席的位置上去。一霎时，北京谣言四起，个个都说胡锦涛已经下了决心要干掉习近平这位养子太子，另扶亲生儿子李克强登基。连带着，大家也都说，老江这回真是「过气」了，眼睁睁地就要看着自己立的太子被本是儿皇帝的阴谋家给斩于马下而无可奈何。 <br />
<br />
不管这话是真是假，只要大家都这么说，事情就能办成一大半。为什么呢？原因很简单，第一，江胡也好，习李也好，其实没有一个是真有本领真有基础的，都得靠着位势把人聚拢起来，方能坐得住天下。第二，如今这批本党高级干部，都是些有奶便是娘的货色，要看你的位势他才聚拢过来。这样一反一正，内里学问就很大了。所谓有奶便是娘，你总不能喝到了奶之后再来认娘吧？你不认娘，人家有奶不给你喝，想喝的多著呢。可是，还没喝到奶，认错了娘怎么办？所以这里有个诀窍，中国当今官场的最大诀窍，那就是要会隔皮猜瓜，瞄一眼就知道这位下不下奶。说句不大文明的话，那就等于有大奶子的就占便宜了。人家鼓鼓地往那儿一站，乳沟老深，大家都看准了：这位有奶，赶快认娘吧。你想，这么多我党高干都来认娘，那是什么资源?!就算本来没有奶，怎么整还不能整出奶来？大不了加点儿三聚氰胺嘛。 <br />
<br />
　　这个逻辑你听明白了吧？也懂得为什么隆胸手术很受欢迎了吧？江泽民四中全会摔了一跤，奶子摔瘪了，眼看自己的儿子就要饿死，这么多年认领的无数位列政治局委员、省委书记等高位的那群儿子们就要另认新「娘」去了。于是乎，必须快做隆胸手术，向党内外显示本核心的奶源其实是丰沛无比、源源不断也。十月一号这一天，当众做的就是这个手术。 <br />
<br />
　　习近平单刀走西欧 <br />
<br />
老江毕竟年纪大了，一次手术不一定管用。而且，毕竟已经不在其位，那些时时刻刻巴结第一把手的我党高官们，究竟是不是信服这一手，还是更多地愿意投靠在当今天子胡锦涛门下，那是不好把握的。为了保险，还得吃点儿催奶药。于是乎，我们看到了一个不寻常的动作，那就是习近平出访德国时特意代表江泽民向德国总理默克尔送书。一时舆论哗然，连近几个月信息封锁厉害的北京也听到了不少相关的议论。有人说，这是习近平公开亮出自己的江系身份，是要和胡锦涛摊牌。这其实是不了解我党内情的看法。试想，习近平是不是江系身份，这在中共最高层，特别是对于胡锦涛来说，难道是什么秘密吗？根本不是秘密，当然也无所谓亮出身份摊牌之说。或者说，亮出身份的含义是有的，但不是要给胡锦涛看，而是要给中共高中层干部们看，给中共军队的高级将领们看。在我党的政治运作中，这些人并不一定清楚地了解这些最高层之间的关系。而现在呢，江泽民认为应该让他们了解这一点了。了解了，有什么好处呢？答案很简单：你不认我这有奶的娘？你看看，习近平就是我的亲儿子，所以他能当上皇储。你觉得四中全会他不行了？你看错了！你看看，我完全支持他。只要有了我的支持，喝上了老娘的奶，你就强壮无比，迅速升官。 「晓得不晓得？」 <br />
<br />
当然，胡锦涛仗着在其位，不用隆胸也可以收拢要喝奶的，就不必这么高姿态了。他善于来阴的。习近平这次出访欧洲，最最奇怪的是，官方报道中完全没有提到他有任何随行人员，好像整个单枪匹马杀出去似的。对比之下，十月底李克强出访澳洲，那个阵势，有四、五个副部长级随行人员。这种小动作，是胡系的看家本领。管不管用？应该是管用的，否则人家胡锦涛能爬上那样的高位？否则胡锦涛能那样赏识专会弄这套把戏的令计划？ <br />
<br />
　　胡锦涛「便秘」失总理？ <br />
<br />
当然，胡系的看家本领，不等于胡本人的看家本领。胡锦涛毕竟比胡系这帮共青团的小「催本儿」高明一些。至少，人家胡锦涛沉得住气。不要小看「沉得住气」这四个字。政治这个事情，就像便秘，关键是要沉得住气；坐在那里时间长了，总能拉出点儿什么，总能制造一点儿臭味，而且这样的臭味往往浓密而经久不息。反正，跑肚子的人是搞不了中国政治的。一泡尿憋不住，都可以因此丢掉中国社科院的院长这样一个高于正部级的大好官位呢。 <br />
<br />
从「经营十八大」这个宏观层面看，胡锦涛沉得住气，就是要等江泽民死。你老江有很强的企图心，要在十八大继续主导高层人事安排？想得美！离十八大还有三年时间呢，你一个已经八十三岁的骚老头，谁敢说在这期间不会去见阎王？七十三，八十四，也许明年就是你的死期。这一番胡锦涛的心里话，虽然没有说出声来，其实谁都听到了，而首先听到的就是江泽民。所以，老江根本不大敢在北京常住，更不要说在北京看病、住院。 <br />
<br />
为了这个目的，在当下的具体人事安排中，一方面，胡锦涛使蔫儿劲，要见缝插针地安排自己的人马。不要忘了，便秘并不意味着端坐那儿不用力气，其实是要使更大的劲儿。不过，因为端坐在那里，外表上不大看得出来，所以叫做「蔫儿劲」──除非使劲使到把脸都憋紫了，外人方才略知一二。你看如今中国官场，薄熙来在重庆唱红歌，李克强、周永康到井冈山磕头，李长春去韶山拜神，这都属于使劲使到脸也憋紫了的地步，红到发紫，就被咱老人家看出他们屁股底下的努力来了。再说另一方面，也不能太高调，那就不符合「沉得住气」的基本原则了。江泽民但凡恶声恶相地要给他的某个马仔喂奶，锦涛同志也就会俯首称是，心里只在盼望着老不死的奶水流干，得了好处早些翘辫子。把这两个方面合到一起，那就是在胡锦涛时代的中国官场所盛行的「便秘战略」。 <br />
<br />
终于说到开头的话题上来了：这次的省级人事变动，就是典型的「便秘」。首先，这次变动是憋了一个多月才弄出来的。那几个届龄退休的省委书记，即河南徐光春、辽宁张文岳、内蒙古储波，本来，按照这几年的习惯，应该在满六十五周岁之前一两个月、甚至还要再早一些，就被安排免职。当然，同时也就任命新的省委书记了。可是，这一次，拖拖拉拉，拖了至少一个多月。为什么呢？一个困于便秘，一个急于催奶，中共这个八十八岁的老头子，那个身体内部实在是处处不适也。说白了，四中全会之后，江系败了一局之后必定寸土不让，胡系则要乘胜追击但又有所忌惮，这类人事案就比以往相较而尤其难产了。 <br />
<br />
第二呢，就是胡锦涛的「蔫儿劲」见效了。四十六岁的胡春华，在河北当省长才一年稍多，任上还有「三鹿奶粉」这样的特大丑闻，却被一把提成了内蒙古的书记，已经跑到了他在共青团中央的前任周强前面。我老人家相信，周强同志其实是面对这次人事调整最不开心的一位。想一想，自己被「锦涛干部学校」的小学弟超前了不说，而且，在湖南省长这个位子上，自己明明比那学弟在河北省长位子呆了长的太多的时间了，政绩政声按说也都比他强，怎么这同一个娘生的也有亲疏厚薄呢？周强同志明后年当然有机会升任省委书记，但是，这次被人从头上迈过去，官场上谁还不明白，自己的亲娘都不给自己喂偏奶，将来这姥姥不疼爷爷不爱的漫长仕途怎么混呀？ <br />
<br />
要知道，胡春华可是胡锦涛和李克强的双重嫡系。一九八四年，胡春华从北大中文系毕业的时候，要去西藏，那是出身北大团委书记的李克强所刻意树立的典型。到了西藏一些年之后，不料想锦涛同志也来了，克强同志的两个大小朋友就在这世界屋脊上握手了。这样两重缘分，决定了胡春华铁定是胡锦涛所属意的所谓「第六代」接班的第一号人物。 「第五代」究竟怎样接班，形势还不明朗呢，这「第六代」已经上了快车道。这中共官场，因此也就成了杭州飙车案，那车开得贼快的少爷，还说自己才「七十码」，压死谁谁倒霉。 <br />
<br />
然而，还有第三，那就是，比胡春华还年轻半岁的孙政才，这次也同样出任省委书记了。这个孙政才，是个神秘人物。神秘就神秘在从政履历太简单，却能在官场上遥遥领先那些老鸟。三年前，他四十岁出头，从北京市委秘书长的位子上，一下子冒升国家农业部长，当时被胡系的人恨得咬牙根，后来，他原来在农科院的搭档莫名其妙地被人杀死了，谁也不知道发生了什?事情。 ──是不是为了收集他的材料而走到这一步，这就不是我老人家所能了解的了。很多人认为他的派系背景不太清楚，其实这是故意模糊视线。中国农业科学研究，在整个九十年代和本世纪最初几年，你说是谁家的天下？江泽慧同志嘛！至于北京市，在陈希同倒台之后直到如今，你说又是谁家的天下？贾庆林同志嘛！江泽慧、贾庆林背后是谁，不用我老人家明言了吧？孙政才这次调任吉林省委书记，显示了老江确实还有产奶的能力。不用说，这已经为孙将来竞争所谓「第六代」的总理乃至总书记创造了领先地位。你便秘，我也不客气；「第五代」我选总书记你选总理，你还不满意，还想搞掉我选的总书记；那「第六代」嘛，你选总书记，我也要选总理，说不好届时我的总理也要搞掉你的总书记。中国的官场人事棋局，大体就是这个意思。 <br />
<br />
这样来说，这次省级人事变动，也不能说是太小的事情，因为其中埋伏着我党「第六代」领导人的接班阵营呢。可是，在我老人家看来，什么「第五代」、「第六代」，统统放屁。天下难道真有不散的宴席？老毛有诗曰：「不须放屁，试看天地翻覆」<br />
