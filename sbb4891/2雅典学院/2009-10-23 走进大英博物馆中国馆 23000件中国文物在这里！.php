													<h2>走进大英博物馆中国馆 23000件中国文物在这里！</h2>
						
						
																											<div id="postmessage_297663" class="t_msgfont"><a href="http://shaanxi.cctv.com/20091023/102185.shtml" target="_blank">http://shaanxi.cctv.com/20091023/102185.shtml</a><br />
<br />
<img src="http://shaanxi.cctv.com/20091023/images/1256265501168_f3282af10b.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
收藏中国流失文物最多的博物馆。联合国教科文组织的数据表明，中国流失文物多达164万件，被世界47家博物馆收藏。大英博物馆是收藏中国流失文物最多的一个博物馆，其收藏中国文物的历史可追溯到1753年建馆时期，目前收藏的中国文物多达2万3千件，长期陈列的约有2000件。<br />
<br />
<img src="http://shaanxi.cctv.com/20091023/images/1256265501175_ab4e80d42f.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
大英博物馆中国馆<br />
<br />
　　这些藏品与古希腊、古埃及藏品等一并成为该馆收藏的最重要最珍贵的人类文化遗产。大英博物馆的33号展厅是专门陈列中国文物的永久性展厅，与古埃及、古希腊、古罗马和印度展厅一样是该博物馆仅有的几个国别展厅之一。<br />
<br />
<img src="http://shaanxi.cctv.com/20091023/images/1256265501182_7df5a07dbd.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
大英博物馆中国馆<br />
<br />
　　该馆收藏的中国文物囊括了中国整个艺术类别，一言以蔽之，远古石器、商周青铜器、魏晋石佛经卷、唐宋书画、明清瓷器等标刻着中国历史上各个文化登峰造极的国宝在这里皆可见到，且可谓门类齐全，美不胜收。<br />
<br />
然而，这仅仅是大英博物馆收藏的2万3千件中国历代稀世珍宝中的一部分，另外的十分之九都存放在10个藏室中，除非得到特殊许可，一般游客是无缘谋面的。<br />
<br />
<img src="http://shaanxi.cctv.com/20091023/images/1256265501195_82c71d550b.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
《女史箴图》局部<br />
<br />
　　某些藏品，如东晋顾恺之《女史箴图》的唐代摹本只有特别的专家才可获得机会一饱眼福。《女史箴图》是当今存世最早的中国绢画，是尚能见到的中国最早专业画家的作品之一，在中国美术史上具有里程碑的意义，一直是历代宫廷收藏的珍品。<br />
<img src="http://shaanxi.cctv.com/20091023/images/1256265501200_3f87723d67.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
现在世界上只剩两幅摹本，其一为宋人临摹，被北京故宫博物院收藏，笔意色彩皆非上品。另一幅就是大英博物馆中的这件摹本。它本为清宫所藏，是乾隆皇帝的案头爱物，藏在圆明园中。1860年，英法联军入侵北京，英军大尉基勇从圆明园中盗出并携往国外。<br />
<br />
<img src="http://shaanxi.cctv.com/20091023/images/1256265501207_cb48550188.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
1903年被大英博物馆收藏，成为该馆最重要的东方文物，称之为“镇馆之宝”毫不为过。该摹本存放于馆内的斯坦因密室，据报道，南京师范大学敦煌学研究中心艺术研究室主任谢成水2002年曾偶然在该密室看过这幅摹本，当时在登记册上只有上个世纪20年代两个日本人来现场临摹过的记录。<br />
<br />
<img src="http://shaanxi.cctv.com/20091023/images/1256265501213_9a3d50aeaf.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
在中国厅中央墙上有几十平方米的敦煌壁画，其割痕虽犹可见，却难掩其久远的鲜丽及三位“浓丽丰肥”菩萨的雍容华贵。大英博物馆收藏的国宝级敦煌画卷及经卷多以万计，除了这幅壁画，其他藏品在中国厅内却难觅踪迹。<br />
<br />
<img src="http://shaanxi.cctv.com/20091023/images/1256265501218_05ffe9e77a.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
1856年到1932年间，多个所谓的“西方探险家”以科学考察为名深入中国西北地区达60多次，每次都掠走大量的文献文物。其中，尤以1907年匈牙利人斯坦因和法国人伯希在敦煌藏经洞劫掠的文物最多。<br />
<br />
<img src="http://shaanxi.cctv.com/20091023/images/1256265501224_8fd86cf4c1.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
敦煌藏经洞的4万多件经书书稿中，大英博物馆就藏得1.3万件，中国现存仅三分之一，致使学术界有“敦煌者，我国学术之伤心史”之嗟叹。或许是因为斯坦因的“贡献”巨大，大英博物馆专门将存放中国古画的藏室以斯坦因命名。<br />
<br />
<img src="http://shaanxi.cctv.com/20091023/images/1256265501230_150f469a92.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
《永乐大典》<br />
<br />
　　此外，国宝级的中国波罗密佛经的最早版本、宋罗汉三彩像、宋明名画，45卷《永乐大典》等都被大英博物馆收藏。据说如果加上1973年并入大英图书馆的部分，该馆收藏的中国书刊有6万多种。<br />
<br />
<img src="http://shaanxi.cctv.com/20091023/images/1256265501235_ad07377667.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
藏品展出的政策和现实<br />
<br />
　　根据大英博物馆的统计，该馆拥有七百万件文物，每年接待500万名参观者，其中有多少人到中国厅参观很难统计。但只要去参观就不难发现，中国馆的参观者有相当一部分是华人，尤其是近年来到英国的中国团组越来越多，参观大英博物馆中国厅成为团组在伦敦必不可少的活动。其他国家的游客也有，往往只是在这个位于博物馆尽头的中国厅匆匆一瞥。<br />
<br />
<img src="http://shaanxi.cctv.com/20091023/images/1256265501240_97b24ff9fd.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
据介绍，因为英国政府拨款减少，加上游客减少，该馆财力紧张，从1992年以来，中国厅的文物就很少被更换过。财力吃紧还导致职位减少，而与中国文物有关的职位首当其冲。财力吃紧也致使该馆无力开展中国文物的巡展和文化交流。<br />
<br />
