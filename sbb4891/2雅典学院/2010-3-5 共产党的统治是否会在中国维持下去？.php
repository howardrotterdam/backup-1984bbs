													<h2>共产党的统治是否会在中国维持下去？</h2>
						
						
																											<div id="postmessage_503565" class="t_msgfont">原文：Is Communist Party Rule Sustainable in China?<br />
<br />
译文：卡内基国际和平基金会的“重新定义中国政策”系列辩论<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;第一场:共产党的统治是否会在中国维持下去？<br />
<br />
<br />
发言人：黎安友(Andrew J. Nathan)，哥伦比亚大学政治科学教授<br />
<br />
发言时间：2006年10月5日<br />
<br />
译文发表时间：2010年3月4日<br />
<br />
译者：推特ID：@Freeman7777，@hsinwang1982，@jiangge09<br />
<br />
校订：@Freeman7777、@jiangge09<br />
<br />
 <br />
<br />
 <br />
<br />
界定辩论的主题<br />
<br />
<br />
分析中国当前的轨迹与预测它未来将会发生什么是不一样的。未曾料想的事件可以使一个国家脱离任何它正行进的轨道。在中国这种情况经常会发生，而且它很可能将再次发生。例如，中国的轨迹可能会由于美国经济衰退，公共卫生危机，或在朝鲜半岛或台湾海峡发生战争而遭受到阻碍。1<br />
<br />
 <br />
<br />
不过据我所了解，在“中国向何处去？”这个领域文献上的辩论是关于什么是可能发生的，论据则是建立在已经可以看到的那些事实上面。中国是目前面临着诸多挑战并且在解决它们上面表现如此糟糕，以至于它的政治体制很可能会崩溃呢？2（之后会发生什么并没有被这一看法的支持者详述。）或是对于民主的诉求增长得如此强烈，以至于该体制将很有可能民主化？3还是照我所认为的那样，当前在体制中运作的力量，尽管存在着它们是复杂的并且是被拉往各个方向的这种事实，但总体趋势反而是朝着我所称之为的一个具有韧性的威权政权继续掌权的方向在发展？<br />
<br />
 <br />
<br />
换言之，我们谈论更多的是目前而不是未来或是那种对于现在来讲显而易见的那种未来。改变将会发生，但要去论及任何具体的关于这种变化的可能性方面的事情，唯一方式就是去看现在存在的那些力量。我的论点是，足以造成中国出现崩溃或民主化的力量是不到位的，而维持威权政权的力量却是实实在在存在的。我这样讲并不是声称说知道那种情况会持续下去多久。<br />
<br />
 <br />
<br />
说威权体制是具有韧性的，并不意味着我在说它是静态的。这个体制做了众多的政策变化但是只做了较少的，某些结构性变化。调适的能力是体制韧性的一部分。但是直至今日并不存在的一些力量（某些外生性的变化或偶发事件）发生作用了，中国政府目前的轨迹是为了持续的威权统治，而不是为了让体制的性质产生变化。<br />
<br />
 <br />
<br />
<br />
美国的对华政策需要建立在这项现实之上。<br />
<br />
 <br />
<br />
具有韧性的威权主义<br />
<br />
<br />
我把中国的威权政权形容为是“具有韧性的”因为它维持了强大的威权主义的特征并且权力很稳固。该政权具有韧性的一些迹象如下：<br />
<br />
 <br />
<br />
胡锦涛在2002-2003年顺利继承权力并且他自那时开始巩固了他的权力<br />
<br />
 <br />
<br />
该政权在经济和社会问题以及提出政策变化去应对这些问题的处置能力。最近的例子是银行系统的改革以及农村粮食税的取消。这些改革很少顺利实施，但是从长远来看，他们往往有着显著的影响，正如自1978年以来实施改革方案这段历史所表明的那样。<br />
<br />
 <br />
<br />
公共舆论对政权的支持保持在高水平。2002年当在全国性的随机抽样调查中被问及， “有多满意或不满意我国的民主的方式”时，79％的中国受访者表示自己是比较满意或非常满意的，这在8个亚洲国家中是比例第二高的民意调查。（许多中国人接受政权所声称的，中国目前的体制是民主政体形式的一种。）<br />
<br />
 <br />
<br />
社会不满和政治上的异见无法累积。社会失序现象发生，多次被引用的具体证据是2005年所统计出来的84,000起群体性事件，但这些抗议活动并没有连接起来。毛泽东说过的一句名言现在要反过来讲了，“星星之火，现在却没有燎原”，这是由于该政权的“消防队”努力之下造成的结果。<br />
<br />
 <br />
<br />
 <br />
<br />
韧性的根源<br />
<br />
<br />
威权政权紧握权力却仍然安全是因为以下原因：<br />
<br />
 <br />
<br />
经济增长已经改善了多数人口的福利，只要他们继续受益于该政权的政策和稳定，就等于是他们给当前政权投下了继续存活下去的一票。<br />
<br />
 <br />
<br />
该政权在外交政策上取得了许多可以感觉到的成就，如确保2008年奥运会在北京召开，在广泛议题上对抗美国和日本，（在中国公民的看法里）有效遏制了台湾总统陈水扁支持独立的举动。这些成就在一个有着民族主义情绪的大众中产生出了一种自豪感。<br />
<br />
 <br />
<br />
该政权维持了一系列的具有安全阀功能的机构，尽管它们是无效率的，但还是给不满的公民提供了一种反对执政党的替代选项。这些措施包括了信访体制，法院体制，行政诉讼体制，以书面的方式给媒体写信，以及政府监视和操纵下的互联网聊天室。<br />
<br />
 <br />
<br />
该政权一直能够使用镇压手段去防止任何实质性的政治反对派兴起。它以部分让步收买了示威者，与此同时则逮捕了示威的组织者。当面对有决心的，像中国民主党或法轮功这样的组织化对手时，在观察了一段时间之后，就逮捕和囚禁了相关的活动人士。该政权已经对庞大的网络警察和技术做了投资，迄今为止使它既得以避免了被有心人士利用网络去进行协调组织又避免了激起自发运动的火花。<br />
<br />
 <br />
<br />
该政权一直掌控着广泛的和复杂的宣传系统，在这样一种方式中广大公众感受到了媒体中的多样性和显著的自由，而在同一时间敏感的政治讯息被从公共领域中消除掉了。6<br />
<br />
 <br />
<br />
党已经发展出了能够笼络经济和社会精英的能力，所以对于有雄心壮志的人来讲“为了变得更好这是唯一的选择”(“the only game in town”)。要么是通过党员身份要么是通过非正式的侍从关系，大多数企业家和有抱负的知识分子都在为党所用。<br />
<br />
 <br />
<br />
该政权拥有必要的决策体系来应对经济和社会变迁。虽然地方官僚机构可能已经腐化，即便这样该政权在中央和一些地方机关内部还是聚集了一批相当符合标准的技术官僚来收集情报，供养着专家和智库，并在内部进行商榷，使官僚层级里的利益相关者的声音被听到。政府有能力辨识主要的问题（例如，在金融系统中的，在环境政策中的，农民负担方面的，社会福利安全网）并且经常（但并不是绝对）做出技术上熟练的决策。此外它往往能能够达成决策的目标，虽然并不总是能够在官僚机构中执行他们。<br />
<br />
 <br />
<br />
对政权的韧性至关重要的是精英的权力意志。领导层是团结在一起的。如果它是分裂的（正如Bruce Gilley所表明会发生的那样），那么无论是经由崩溃或是经由民主转型，我同意政权会很快衰落这种说法。但是，领导层已吸取了64事件以及“苏东波”（苏联，东欧和波兰的共产政权的衰落）的教训认为对其所有党员来讲最重要的事情是不要表现出不团结。正如毛泽东所说，“在所有重要的事情中，拥有权力才是最重要的。”8为此，精英制定了限制政治冲突强度的规范。这些规范包括，最高领导人在70岁退休，最高领导人拥有在他们所掌控的领域内做出决策的权力，重要决策都以集体的方式决定，并且已退休的政治老人不干预政治。因此，在江泽民和胡锦涛的统治下，领导人已经以这种方式处理了他们的关系，使得权力上和政策上都没有破坏性的分裂产生。<br />
<br />
 <br />
<br />
<br />
趋势<br />
<br />
<br />
当然，也有着强大的社会趋势去挑战这个政权。这些社会趋势包括了：<br />
<br />
 <br />
<br />
经济的脆弱性。金融系统十分薄弱。国有企业仍然是享有特权，经营效率也依然是低下的。经济增长过于依赖出口。经济面临着能源危机。在制约投资上该政权也有着麻烦。<br />
<br />
 <br />
<br />
威权式统治在提供公共服务、处理突发公共卫生事件和自然灾害方面，削减腐败，以及处理其他“治理”议题上的弱点。9腐败的征占土地一直是农村和城市不满的强力来源。腐败导致了对于环境的损害和煤矿灾害，以及其他许多弊病。该政权还面临着不断加剧的供水危机。<br />
<br />
 <br />
<br />
继续存在着一个庞大的农民工阶层，他们只有着二等阶级的地位，也很不满意这种现状，以及只是勉强维持了生计的国有企业下岗、待岗工人阶级；大部份农村地区依然落后；独立于国家控制之外的宗教运动有所增长。随着权利意识的崛起，弱势群体似乎迫切的更为积极得发出他们的主张。一个缓慢成长的律师以及其他活动人士队伍正在帮助他们。虽然我上面提到过社会大众普遍支持现政权，但还是有千百万的严重不满现状的人存在。<br />
<br />
 <br />
<br />
在这些情况的背景下，民众的支持是脆弱的。只要他们认为这一政权正在运转，并决心抓住权力不放人民就会是信服的。但如果经济虚弱下去，国家输掉战争，或是政权在面对挑战的时候会显露出犹豫不决，公众的支持就可能蒸发掉并且许多被压制的批评和不满会浮出表面。总之，一个威权政权相比民主政权更容易受到“权力紧缩”(“power deflation”)的影响，因为在保有权力上它是更依赖于强制性手段的。这个政权处在一种如此脆弱的边缘，解释了为何它表现得如此害怕受到伤害，以至于需要通过采取过度反应的措施，例如允许地方当局草率地判决一位试图利用司法体系去保护当地妇女免于遭受计划生育政策强制性虐待的盲人法律维权人士（陈光诚）入狱。<br />
<br />
 <br />
<br />
虽然政权面临着这些挑战，我们想要了解关于一个威权体制持久性方面的事情，以“从上至下”的视角去进行观察会比以“从下至上”的视角进行观察了解到更多。这类政权不是被推翻的，它们只是衰落了。只有当他们允许它发生的时候才会出现这种情况。可以肯定的是，威权崩溃的病因论涉及到了一种因果性的循环，其组成步骤是难以理顺的。一个威权政权如果发现自己无法处理反对浪潮的兴起它就可能放弃对权力的控制。但是如果该政权以总体来讲有效的新政策去加以回应、以暴力镇压了反对派并且使得政权内部的分歧局限在一定地步的话，反对浪潮就不会上升到那么高的地步（使政权甘心放弃权力）。该政权并没有面临到有能力去质疑其对权力进行掌控的组织化的反对派，这是其自身的行动而不是因为缺乏问题所形成的结果。<br />
<br />
<br />
为了在未来生存下来，这个政权将不得不继续应对新的挑战，那种新挑战毫无疑问将不断出现。中国是巨大的，快速变迁的，并容易受到各种意外事件所影响的。如果其被视为表现不佳，软弱或分裂的，或者如果它失去了对提供给公众的信息的管制，或表现为在镇压方面是低效率的，那么该政权的权力意志将会失去信心。它就会心怀恐惧的去考虑政府必须做什么才能生存下去，继续提供经济增长和民族自豪感方面的成就，控制互联网，保持其领先于社会、经济、环境问题的政策，维持良好的国内情报工作，有效地执掌并遏制其内部的分歧和对立。然而，这正是该政权当前正在做的事。<br />
<br />
 <br />
<br />
实现所有这些目标的努力将不可避免地导致统治体制的某些逐步的“现代化”吗？那样的话政权会以被一些学者所认为的已经形成“计划外的成长”(“grow out of the plan”)同样的方式形成“脱离威权主义轨迹的发展”(“grow out of authoritarianism”)吗？目前没有证据表明这点。该政权的制度性变迁，到目前为止，是有助于巩固而不是削弱威权主义的。建设“法治”的计划已导致了法庭是屈从于党这种结果的。11乡村选举的制度已经透过寻求不被村民接受的地方领导人加强了政权对于农村的掌控。媒体的多样化和互联网的普及已经使得政权更为有效的散布了其世界观。12<br />
<br />
 <br />
<br />
 <br />
<br />
理论视角<br />
<br />
<br />
在关于中国潜在未来的讨论中，我无法赞同在我看来是被误用的三种理论视角。按中文的风格，我把本人关于威权体制韧性的讨论概括为三个理论上的“未必如此”。<br />
<br />
 <br />
<br />
第一个“未必如此”，是认为一个崛起的中产阶级会要求民主(该理论来自于西摩尔・马丁・李普赛特）。但当中产阶级担忧一个人数比其更加庞大的农民及农民工阶层，而把威权主义的政权视为防备混乱维持秩序的堡垒时，它是不会要求民主的；当中产阶级被这个政权笼络，并靠与其结盟而获益时，它是不会要求民主的；13当中产阶级将民主化视为外国政府用来损害中国的崛起与在国际经济体系中的竞争力的阴谋（一场“颜色革命”）时，它是不会要求民主的。鉴于这些情况，民主化的阶级论 一般在这个语境下都翻译为阶级论就并不适用于今日的中国了。<br />
<br />
 <br />
<br />
第二个“未必如此”， 是认为在现代经济与社会中（无论是工业的、后工业的，还是信息化的），政府必须允许自由才能保证使社会能够自我调节、经济能够有效运转（该理论来自于 1950年代的社会学家塔尔科特・帕森斯的功能主义理论以及今日的纽约时报专栏作家托马斯・弗里德曼的金色双拱的全球化理论）。当其能够在开放的全球经济中凭借着廉价劳动力的优势实现高水平的经济增长，维持着一个不可自由兑换的货币体系 来使其经济绝缘于全球金融市场，其经济统制制度能够相当成功地（即便没有达到最大效能）管理好国内经济，并且能够投入相当资源用于控制信息，在满足经济类 信息自由流动的同时阻止住敏感的政治类信息的流动，这个政府就不需要满足关于自由的诉求。中国的威权主义政权设计出，并到目前为止成功运行着一个将市场功 能与对企业与产业部门的政府支配与控制结合起来的体系。而这一切，都是在美国及其开放的消费者市场的至关重要的帮助下完成的。面对以上情势，可以看出民主 化的功能论也不适用于今日的中国。<br />
<br />
<br />
第三个“未必如此”，则是认为对全球文化的普遍接受，或是人权与民主规范的散布，会造成民主化，因为这种人与社会的观点已经被其自身证明是正确的（这个理论来自于佛朗西斯・福山，Thomas Risse和Kathryn Sikkink，以及John Meyer的“社会学制度主义”14）。在公民群体感到国家因为某些隐秘的原因被这种全球文化包围时，当一个有着悠久的、令人骄傲的“中心主义”(“centrism”)传统的文化不愿被“去中心化”(“de-centered”)，而更愿意发展出自己的一套非西方的“另类现代性”取而代之的时候，15以及当这个政权通过宣传手段保持着对意识形态与公众文化的控制时，这种全球文化的散布就不会造成相关规范的变化。据上述情形来看，这种全球适应或民主化的规范扩散论(norm diffusion theory)也同样不适用于今日的中国。<br />
<br />
 <br />
<br />
<br />
对美国的政策启示：它为何关系重大<br />
<br />
<br />
在他即将出版的书中，孟捷慕(James Mann)提到美国的对华接触接触政策到目前为止是基于两个自我安慰的想象，其一是这种接触会导致中国的崩溃，另一个则是认为它会带动中国的民主化。16孟捷慕认为我们的政策应当建立在中国当前的威权主义政权会维持下去的这种预期之上，我深以为然。此外，如果我们继续通过市场准入、投资及技术转移来为其经济增长做贡献，这个政权只会变得更加强大、更富影响力。<br />
<br />
然而，我也不能就此推断道我们应当采取减缓中国的经济增长、颠覆这个政权，或者分裂这个国家的政策。在美国，这种政策缺乏有效的政治支持因而不可能奏效，更何况它们还有可能带来预想不到的负面后果，如贫困、动乱、难民以及国际冲突。与此同时，美国政府要让公众相信其中国政策只是作为改变中国的手段也要付出代价。中国的决策者会倾向于相信这种声明并对美国的政策产生比其实际更大的怀疑，而美国公众也会对这样的结果表示失望。<br />
<br />
<br />
实际上，在我看来，中美两国在亚洲及全球经济中的核心安全利益有着广泛的相容性。17自尼克松以来的7届美国政府以这样或那样的方式实施的接触政策使得两国发现并实现了在许多共同利益领域的合作。一个后威权体制时代的中国政府对美国来说也许就没有那么好打交道了，而如果其无法贯彻决策，对民族主义的公众压力无法抗拒，或者无法控制住社会中的次国家行为体(sub-national actors)，打起交道来或许会更难。<br />
<br />
<br />
关于中国威权主义的政策启示，简而言之，就是不要把“中国威胁”与美国国家安全利益联系起来。更确切说，除了国家安全问题外，中国现政权的一系列真正需要我们关切行为会在三个层面影响到美国的利益：我们在中国工作与游历的能力，我们对涉及到与中国相互依存关系的问题的解决能力，以及我们正在建立的全球化处于什么样的层面。<br />
<br />
第一个层面。在中国国内，政府官员压制性的、专横的和腐败的行为不利于外国人做生意、从事新闻采访、学术研究、非政府组织以及旅行等方面的活动。由于缺少独立的司法体系，中国不得不通过政治恩庇(political patronage)来继续吸引外资，但对投资者和出口商来说，这种保护比法治更加昂贵，而且更加低效。小投资者和贸易商，尤其是华人，经常要为缺少司法保护承担风险，他们面对的是搜查、逮捕或事实上的盘剥。白种人通常会得到更好的保护，但情况并非总是如此。由于受害者不愿意公开自己的遭遇，这类案例并不广为人知。投资者也许会因保护的高回报而接受高风险，但在一个公正的司法体系内，他们的境遇会更好。同样，中国亵渎法律的威权主义对于在中国从事学术研究或以中国为研究对象的学者是不利的，对记者、基金会工作者、环保人士、公共卫生工作者和游客同样如此。同样在这种情况下，中国人（即使是外国公民）的处境要比白人差，但不完全是这样。<br />
<br />
 <br />
<br />
第二个层面。在相互依存的层面上，镇压和腐败通过诸如全球卫生、环境、犯罪以及移民等问题影响到了来自美国和其他国家的投资者。对异议和新闻自由的打压加速了一种流行病的传播――它跳出了中国边界去侵染他国以及其他跨越疆域的全球性议题。<br />
<br />
 <br />
<br />
第三个层面。在全球化问题上，中国政府的行为涉及我们能否打造一种对人友善和对环境友善的全球化。中国借助“威权优势”创造了一个损害人权和环境的全球化模式。中国榜样的成功之道正在对世界上其他政权施加越来越大的影响力。<br />
<br />
 <br />
<br />
这些问题是十分广泛的，但有一个主题可以将它们联系并组织起来，这就是人权。18之所以这样说，因为人权不仅事关保护异议人士。如果我们正确地理解人权，它包括一系列基本的权利要求：身体健全、不受歧视、自由、健康、福利以及教育。如果你登陆一般意义上的人权组织网站，你会看到它不仅涉及持不同政见者的权利，还包括一系列其他当下紧迫的问题――例如在中国，它们包括艾滋病、环境、土地强占、教育机会、虐待妇女、矿难以及流动人口的不公待遇。持不同政见者的情况经常与其他问题重叠，因为异议人士正是为了寻求艾滋病、环境问题的解决才成为了异议人士。<br />
<br />
 <br />
<br />
我把自己限定为“现实主义者”或是基于自私的原因才关注中国威权主义的特点，因为我想知道中国的威权主义为什么事关美国。在为什么中国压制性的国家行为与我们事关重大的问题上，我刚才说是基于自私的原因那并不意味着道德上和法律上的原因是不重要的，名义上来讲，那样的行为都是不道德的和非法的。但是我希望我已表明过这样的观点，道德的和法律的理由不是与实用主义的和利己主义的理由相对的，而是与它们一致的。在一个联系越发紧密的世界，像中国这样幅员辽阔、有影响力的大国普遍性的侵犯人权的复杂后果，使外部世界的重大利益也受到了伤害。<br />
<br />
 <br />
<br />
 <br />
<br />
对美国的政策启示：要去做什么<br />
<br />
<br />
从政府、基金会、非政府组织到商业，美国各层面的政策制定者都应该努力为改善中国人权而工作，即使威权的中国政府进行抵制。这样做之所以可能，是因为：<br />
<br />
 <br />
<br />
即使完全实现人权意味着政权更迭，但出于自己的需要，中国的威权政权对人权的一些重要部分仍是有需求的。19在计划生育、土地使用期限、安全生产、环境保护以及公共卫生标准等方面，中央政府并不能从地方官员的腐败和不法行为中获利。中央政府需要人权的某些部分来打击腐败、为公众批评滥权的地方官员提供安全阀、改善公共卫生和计划生育，以及改进中央对地方的检查工作。当然，对中央政府来说，国际上所承认的人权的其他方面是他们无法接受且加以抵制的，如充分的宗教自由、出版自由和政治自由。<br />
<br />
 <br />
<br />
对于地方官员侵犯人权的行为，中国政府并不总是完全知晓。过去，中央政府只是在从国内异议人士和国际组织、国际媒体得知消息后才对人权议题作出回应。<br />
<br />
 <br />
<br />
中国政府关注联合国以及其他国际人权领域的进展，并极力避免遭到谴责。颜面扫地、打败外交对手赢得外交盟友方面的外交和经济成本，以及被政府的国内外批评者所使用的正在演变中的规则的齿轮效应都会让中国政府感到担心。尽管回应得缓慢且不全面，中国政府还是会对施压做出回应。<br />
<br />
 <br />
<br />
在中国崛起以及中国表现出具有韧性的威权主义这种情况下，美国原有的用来施加谴责和威胁制裁的双边性的政府对政府的战略不再有效。但我们有前景看好的战略可供使用。尽管它们需要我们付出努力且效果迟缓。它们包括了：<br />
<br />
 <br />
<br />
政府和非政府组织对联合国机制（条约机构、特别机制）更加充分和认真地利用。<br />
<br />
 <br />
<br />
与众多以基金会和非政府组织为背景的中国公民社会加强合作，包括加大对中国独立律师和维权人士的支持。<br />
<br />
 <br />
<br />
在联合国人权事务高级专员、学界、基金会已开创的局面的基础上，在政府层级要有更多“建设性地接触”(“constructive engagement”)，为身在中国在不同领域里都理解到需要人权的机构或个人提高工作效率打造相应的能力。<br />
<br />
 <br />
<br />
在多边外交方面投入更多的时间和精力，也就是说，针对联合国人权理事会(UN Human Rights Commission) 在人权外交措施(human rights démarches)、对话以及提出解决方案等等事务中与盟国进行合作以及协调。<br />
<br />
 <br />
<br />
这些政策需要一些观念的调整，我认为这对美国政治体系去承受来讲是有难度的。推动人权的事业应与推动民主的事业分离。对中国来说，后者威胁着国家安全。这与美国将权利视为民主要件的传统观念来讲是相反的。推动人权应以国际法而非美国价值观作为基础。这对于一个否认国际法效力且其自身就在违反国际法的政府来说是困难的。我们的政策应寻求广泛意义上的人权，而不应只局限于公民权利、政治权利等许多美国人眼中的所谓的真正人权。并且如果国会议员着眼于长远去推动一个体制里的互不关联的变化，这种互不关联的变化将是以它自己的节奏、最好是慢慢变动的方式去进行，从而去约束他们的言论并使新政策确定下来的话，那么这样的政策会取得更大的成功。<br />
<br />
 <br />
<br />
长期以来，人权一直是美国对华政策的重要组成部分。我的分析说明，这项工作实际上比我们想象得更重要、挑战性更大。如果中国的威权政权短期不发生改变，我们温和但至关重要的目标应该要向中国政府施压，以使其尊重在国际上和国内所做的人权承诺，这样中国和世界才能变得更好。<br />
