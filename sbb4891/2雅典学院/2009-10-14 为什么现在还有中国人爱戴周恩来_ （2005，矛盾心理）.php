													<h2>为什么现在还有中国人爱戴周恩来? （2005，矛盾心理）</h2>
						
						
																											<div id="postmessage_282283" class="t_msgfont">为什么现在还有中国人爱戴周恩来? <br />
刘松萝&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp; 在中国，曾经有亿万个周恩来总理的崇拜者。今天，国人的观念在进步，眼界在开阔，思想在活跃，关于周恩来的评价也随之多元化了。有人认为周恩来胆怯、圆滑、虚伪，还有人认为他助纣为虐甚至残忍。然而让人感到困惑的是，一些对周恩来不利的材料流传以后，很多中国人仍然爱戴周恩来。这是为什么呢？ <br />
<br />
　　我们面对的是周恩来，一个在大大小小的政治灾难中身居要职的人，当然不可能推卸掉应该承担的历史责任。人们现在认为周恩来应该站出来反对文化大革命，然而他没有。因此他不是为民请命的彭德怀，也不是为真理而献身的张志新。那么作为周恩来，什么地方能够让人爱戴呢？ <br />
<br />
　　第一，周恩来没有主动参与发动政治运动，在政治狂热中尽量保持冷静，减少政治运动给国家和人民带来的灾难。前面说过，他不是彭德怀，因此在这方面不可能得到对彭德怀那样的赞誉。但是作为在特定位置上的周恩来，这样做已经是难能可贵了。周恩来有过从事地下工作和情报工作的历史，但他在执政以后尽量不采用激烈和极端的措施，特别是没有用这样的措施对付威胁到自己权力的人。如果周恩来一心为了保住自己的权力，那么最好的办法就是在运动中推波助澜，他不这样做，当然只能是出于良好的动机。 <br />
<br />
　　第二，周恩来亲民和爱民，或者给公众以亲民和爱民的印象。他关注民众的疾苦，并且在政治动荡中倾全力维护国家的稳定，以保障国计民生。1966年3月，河北省邢台地区发生强烈地震，周总理在第二天就乘直升机到灾区视察。当他踏上灾区的大地的时候，余震还没有停止。 <br />
<br />
　　第三，周恩来为人谦和、平易，礼贤下士。周恩来有着传奇般的经历，有足以自傲的资本。然而，他至少在公共场合表现出应有的礼节和谦逊，对于学者和知名人士表现出应有的尊重。在可能的情况下，他常常对学者和知名人士及其亲属加以保护和照顾。 <br />
<br />
　　第四，周恩来不但勤政，而且有很强的行政能力。他在做一件大的事情之前，总是能够考虑到以后的几步棋，在细节上安排得较为周密。 <br />
<br />
　　身处新的时代，我们有了超越周恩来的可能性。人们认识到，制度的完善比依靠能人和善人更加重要。因此，我们看到了周恩来没有尽力阻止政治运动的发生，缺少原则性；我们看到了周恩来尽管有出色的外交才能，但在阶级斗争的外交路线之下，这些才能被用来笼络甚至讨好一些小国；我们也看到了在勤政和能干的同时，周恩来的工作方法中也有事必躬亲，包办琐碎小事的问题…… <br />
<br />
　　我们有了超越周恩来的可能性，那么我们是否超越了周恩来呢？似乎没有。 <br />
<br />
　　人们埋怨周恩来没有阻止政治运动的发生，可是我们再看一看在今天近乎疯狂的GDP崇拜、铺张浪费的竞赛、圈地运动和房地产热潮中，不但没有人像彭德怀那样出来反对，也少有人像周恩来那样试图降低疯狂的热度。因此谁能说周恩来在文革中的作用没有可取之处呢？ <br />
<br />
　　周恩来尽量采用温和的手段处理问题，在今天仍然有借鉴意义。在改革当中，媒体常常过于赞扬那些敢想敢干，大手笔、新思路的领导干部，而这些人很容易走向自己的反面，成为刚愎自用、独断专行，用民众的血汗为自己的错误买单的人。这是一种应该摈弃的文革思维。 <br />
<br />
　　周恩来的亲民和爱民，在今天仍然有借鉴意义。改革和发展中出现的一些偏差，就在于所谓政绩甚至是官僚的个人利益压倒了民众的权益。 <br />
<br />
　　周恩来的为人谦和、平易，礼贤下士，在今天仍然有借鉴意义。现在，官本位已经根深蒂固，一些官员的妄自尊大，目中无人，不尊重知识、不尊重贤者，这样的社会风气已经到了必须改变的时候了。 <br />
<br />
　　周恩来的行政能力更值得我们学习。改革和发展关系到国家的兴亡和民众的福祉，关系重大，必须要有预见性和责任感，不能“走一步看一步”，不能“摸着石头过河”。必须改变那种以目标、概念和口号来代替实际操作的不良作风。 <br />
<br />
　　不理解周恩来为什么仍然受人爱戴，就不可能了解中国的民意和中国的国情。儒家讲求道义，民众盼望清官，这并不是中国文化的糟粕。中国文化的不足，可以通过引进西方自由、平等、博爱的理念来补充，而不是一概否定。曾经有人断言，只要有政绩，只要让“老百姓”生活好，官员的个人品质并不重要。这种犬儒主义的短见，无疑是造成贪污腐化盛行的原因之一。我们可以批评“清官政治”，但万万不可以否定清官。 <br />
<br />
　　不理解周恩来为什么仍然受人爱戴，也不可能真正地了解现代政治。现代政治不依赖完人，不过对于政治家和公务员仍然有个人品德上的要求。轻视对官员个人品质的要求，是一种不负责任的伪现代主义。 <br />
<br />
　　总之，对于否定周恩来的观点，我不强烈地反对。但是，因为反对周恩来就否定他的一些堪称美德的品质，不是正确地总结历史的经验和教训，因而是有害的。<br />
<br />
