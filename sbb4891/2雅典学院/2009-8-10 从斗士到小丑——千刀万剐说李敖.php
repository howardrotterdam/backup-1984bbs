千刀万剐说李敖<br />
??<br />
??<br />
??庄子说“寿则多辱”。此言一度在金庸身上应验，如今正缓缓向74岁的李敖逼近。接受《南方人物周刊》采访，问他身后之事怎么安排，李敖依然是一副玩世不恭的语气：“（墓碑）什么都不写，我死无丧身之地。遗体我会捐给台湾大学医院，千刀万剐。”――最后一句被用作标题，叫“我死后千刀万剐”，那种致命的自负穿越了历史的苍茫，与法国国王路易十五的名言――“我死之后，哪管洪水滔天”――相映成趣。<br />
??对于一个大思想家来说，倘若身体康健，74岁的年纪便远远算不上日暮途穷，但李敖思想的敏锐度之每况愈下，甚至偶有倒行逆施之举，已经是一个不争的事实。访谈当中，每问到关键问题，李敖往往王顾左右而言他，譬如问他对大陆行政体制改革的看法，他则改口辨析“一胎化合不合理”，最后将话头扯到了美国的阿拉斯加。再问他为什么有双重标准，谈到台湾的政治问题，坚持理想主义，谈到大陆呢，则采取现实主义，李敖矢口否认，其辩解却比当前经济形势还要疲软乏力，什么大陆人口比台湾多一百倍，大陆的黄金等值钱的东西都被蒋介石带到了台湾，国民党的终点才是共产点的起点等――这个时候的李敖俨然变身为太极高手。<br />
??整个访谈最精彩的部分，是李敖谈论女人，不乏令人惊艳的妙语，譬如说模特好看，可是不宜上床，因为“太硌了”。谈女人的李敖，以及自吹自擂、狂妄自大的李敖，才是我们所熟悉的那个李敖，才能让我们在恍惚之间重新感受《老年人和棒子》、《十三年和十三月》所遗留的历久弥新的阅读快感。这愈发在印证李敖的悲哀。他终于老了，哪怕他的身体和欲望能够保证他在58岁生一个儿子、60岁再生一个女儿，可他的精神已经追随不上他所经受的苦难与所企望承载的使命。<br />
??<br />
??这个访谈还证实了我的一个判断：当台湾从专制时代转向民主时代，李敖的半边身子便被挤出了民主化的历史舞台，他就像在三千弱水之中苦苦泅渡的生灵，抓住了一根虚空的稻草而努力挽救自己，却力不从心，这是历史的伟力在作用。李敖说过，康有为与慈禧太后的命运铸造在一起，就像一枚硬币的两面，一人死了，另一人的时代即告断裂、终结；他与蒋介石相互捆绑，蒋死了，他的一生则翻过了最快意恩仇的一页，他的青春宣布结束。可以说，他是专制时代最无畏的斗士，却是民主时代最普通的小丑（倒非贬义，如玩马戏的小丑，一种职业而已）。当他在1995年为《蒋介石评传》（与汪荣祖合著）作序发出“我老了”的一声叹息，暗示他从此进入小丑的表演生涯。<br />
??李敖不是不想开辟以他命名的新时代，可我们说了，他心有余而力不足，他的种种挣扎只是在消耗60岁以前所积累的声望。他短促如电的从政生涯，往好了说，叫游侠，往坏了说，叫游戏。他所奉行的还是独行侠式的、英雄主义的那一套古老把戏，以孑然一身，仗剑横行，对抗国民党与民进党两具大风车，说白了就是搅局。那本记录了他担任“立法委员”之三年所见所行的《李敖议坛哀思录》（哪里有哀思呢，他流下的是鳄鱼的眼泪），完全可以换一个名字，叫《李敖议坛捣蛋录》――当然，捣蛋是民主政治的本色。一场游戏一场梦。2007年，李敖宣布不再参加明年台湾的“立法委员”选举，并从此告别政治。梦醒了，他累了，不玩了。<br />
??一个人的心力能有多大？纵然你是天才，却从来超脱不了时代和国度所念念有词的紧箍咒。惟有大唐的豪华盛世才能孕育李白的大才如海，换作在清朝，文字狱肆虐如木马病毒，精神压抑病态青黄不接，充其量只能走出一个怨去吹箫，狂来说剑，豪情与怨气共生的龚自珍。李敖的不幸即在于他生活在了这样一个畸变的时代。尽管享有民国的遗韵，政治专制主义却强烈压迫了他的心胸，小岛之狭决定了他的心气偏安于一隅。待禁锢解除，他垂垂老矣。他毕生追逐民主，民主的幻象却令他衰竭的心力应接不暇。健康运作的民主政治从不需要英雄，只需要小丑。他只好假戏真唱去迎合潮流。然而，他是失败的卓别林。<br />
??<br />
??李敖是旧时代的终结者，却不是新时代的开创者。他是岛国的英雄，却不是大陆的导师――2005年的大陆之行，窃以为最高的评语，只能说这是一位斗士的天鹅之歌。但有一点，正值得引起世人的注意：在鲁迅、李敖与韩寒之间，隐约存在一条连绵不断的血缘，他们对社会之恶的批判从来都是不遗余力，同时他们的生活却极其舒适安乐，甚至豪奢。鲁迅躲在租界安全写作，一年不低于30万人民币的稿费收入，放到今日上海，大约可为中产；《南方人物周刊》的访谈特意指出，李敖所住的豪宅价值达5000万，关于女人，他更是尝尽姹紫嫣红；而除了作家身份，韩寒还是著名的赛车手、歌手以及出版人。这和我们对斗士的传统印象存有何其大的歧异：悲怆、流血、苦行僧、剑拔弩张、牺牲主义，这些词汇可能适用于某一时刻的李敖，纵观他之一生则绝对格格不入。不妨说，李敖为后人提供了一条新的反抗路径：一个快乐的、世俗化的反抗者，肉身与灵魂同样丰盈，对肉欲与正义的追求同样肆无忌惮，对女人与宪政的投入同样令他乐此不疲。<br />
??一面是斗士，一面是小丑，转型时代的传承与分裂注定了这两面的共存亡。千刀万剐，还是那个李敖。</div>

							
							
							
							
