													<h2>曹长青：独立，西藏人民的梦想</h2>
						
						
																											<div id="postmessage_464463" class="t_msgfont">文/曹长青 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;十五年前（1994年），我在《北京之春》发表了题为“独立：西藏人民的权利”的长篇专论，从尊重历史、正视现实和人有追求自由、自治的权利等角度，阐述西藏人民追求自己国家的独立，是合法、合理、合情。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;十五年来，越来越多的中国知识分子关注西藏、为西藏人权呼吁；也有相当一些人走向支持西藏独立。但西藏的精神领袖达赖喇嘛和西藏流亡政府，近年来却一直强调走中间道路，即不走两极：放弃西藏独立，也不接受中共对西藏的殖民统治；承认西藏属于中国，但要求高度自治（包括民主选举）。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;最近在日内瓦举行的“藏汉会议”上，达赖喇嘛到会演讲，仍坚持这一路线。西藏流亡政府的民选总理桑东仁波切的讲话，也是强调这一点，仍期待有机会跟中国政府对话，使西藏获得真正自治，达到“双赢”。和以往不同的是，西藏流亡政府的政策，从重点在西方、关键点在中国政府（谋求和北京谈判），转向了中国民间；尤其开始重视海外中国知识分子，想通过他们传播西藏的真实。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;民主和专制能共存、双赢吗？ <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;我也参加了这次在日内瓦的题为“寻找共同点”的藏汉会议。在桑东仁波切总理致辞后接受自由提问时，就达赖喇嘛和西藏流亡政府的“中间道路”和“双赢”企图，我提出了两个问题：<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;第一：在人类历史上，有没有过专制和民主这两种价值，以及其实践，在一个体制下共存过？甚至“双赢”？如果没有，那为什么桑东仁波切政府在追求这样一个不切实际的方向？第二：中间道路有没有限期（deadline）？流亡政府放弃西藏独立的决定，是不是剥夺了六百万西藏人民和他们的后代对国家前途的选择权？<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;桑东仁波切曾说，他们谋求的是“在中国宪法精神下的真正自治”。而对于“中国宪法精神”，恰好中共统战部副部长朱维群去年底在北京记者会上有说明，他说，中国宪法精神就是“三个坚持”：坚持共产党领导，坚持中国特色社会主义，（针对西藏问题）坚持民族区域自治。对于共产党的领导，谁都知道，就是专制统治；而中国特色的社会主义，是同一回事：经济往哪个方向走，一切由共产党说了算；而民族区域自治呢，朱维群用“排除法”说，不是联邦制，不是邦联制，也不是香港的一国两制，更不是达赖喇嘛的真正自治，而是共产党领导下的、现在的“西藏自治区”制。随后朱维群语气蛮横地说，“在中国搞民族区域自治就是这个搞法，没有别的搞法，没有别的什么‘真正的自治’。”<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;不能期待“善良的狼” <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;朱维群还说，我们从来没承认过西藏流亡政府，只是跟达赖喇嘛的私人代表接触，谈的是达赖喇嘛如何“改正错误”，争取我们的“谅解”。表现好，可以让他回来，“最多再加上他身边一些人”也可回来。面对众多外国记者，朱维群毫无最基本外交礼貌地宣称：过去这些年，中央政府跟达赖喇嘛私人代表的接触，就是谈这些，将来也不会改变！那种霸道，真令人头皮发麻。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;我向桑东仁波切总理请教说，共产党已经把话说得这么清楚，西藏流亡政府还期待跟北京谈什么？跟共产党的专制要“真正自治”，等于期待有“善良的狼”，“羊狼共存、双赢”。人类有过这种先例吗？<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;桑东仁波切回答说，中间路线是经过流亡藏人公投，多数赞成的；并说不久前的流亡藏人代表大会，最后多数仍是支持中间道路，它是民意的体现。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;我则提到，流亡藏人只有13万左右，即使全部都同意中间道路，那也不能代表西藏境内六百万藏人的意愿。流亡政府这样做，是不是剥夺了那绝大多数西藏人未来的国家选择权？<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;西藏问题的根本，不是保存宗教文化 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;西藏历史上是个独立的国家，有自己的国号、国旗、元首、首都、货币、护照、军队、税收等等。即使清朝后期，以及蒋介石民国时代，西藏也不是它们的一个省；只是五十年代初，被中共军队占领了。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;今天，西藏的问题，并不仅仅是宗教、文化和环境保护的问题，而是一个国家的存亡问题。达赖喇嘛作为西藏人民的精神领袖，备受藏人爱戴。但按照民主原则，不管地位怎样崇高的领袖，都没有权力决定人民的国家选择权。西藏的前途，应该由六百万西藏人民公投决定，而不是由西藏流亡政府的任何官员决定！西藏青年大会副主席葛玛益西和议员葛玛秋培等就曾提过，“我们少数流亡藏人无权否定历史上即为独立国家之人民的恢复独立的权力，我们也没有改变西藏民族后代之前途的权力。我们的问题是民族和国家的独立与自由，而不是文化、宗教和环境保护等问题。”<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;现在六百万西藏人民正遭受中共的政治专制和殖民统治，无法发出声音。但从西藏境内多次藏人游行示威的口号可以看出，多数西藏人民要的是西藏独立！过去这些年来，我在印度、欧洲、美国等地直接采访过上百名藏人（和藏人群体接触的人数当然多许多倍），我对他们都问过相同的问题：要自治还是独立。除了达赖喇嘛之外，其他全部都说要西藏独立。我曾当面对达赖喇嘛说，我采访过的全部西藏人都要求独立，只有一个不要独立。他问，是谁？我说，就是你！达赖喇嘛只有用他魅力无穷的哈哈大笑来回答。 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;“有没有可能”和“应不应该” <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;当然，我完全能理解达赖喇嘛的一片苦衷，西藏的文化和宗教正在被毁灭，汉人大量移民西藏，中共推行汉化政策，希望像内蒙那样解决“西藏问题”（今天内蒙2380万人口中，汉人已占80％！而处于少数的蒙古人，据说四人中已有三个被汉化）。有些支持中间道路的藏人说，中共太强大了，寻求独立是不可能的。这说明，他们的思维也被“汉化”，像工于心计的中国人那样想问题，什么事都先想到“有没有可能”，而不是像西方人那样先问“应不应该”。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;我在这里要特别重复、重点强调一个中国人和西方人思维的根本不同：中国人（中国文化培养出来的人）遇到事情，首先想到的是：有没有可能？而西方文化培养出来的人，首先想到的是：应不应该？换句话说，他们首先想到的是原则，是理念。而凡事有了理念，坚持原则，不懈奋斗，成功一定就有可能。所以西方人可以提出“不自由毋宁死”，而不是中国人的“好死不如赖活着”。正是西方人首先想的是“应不应该”，而不是“可不可能”，所以自由和繁荣才最早从西方开始。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;独立、民族自决，是世界潮流 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;犹太人复国，就是典型的例子。他们还不像西藏人这样，一直有自己的家园，而是流散世界，曾长期没有一块安身立命的土地。但他们坚持自己的梦想，经过多年的追求、奋斗，更有流血牺牲，最后终于复国，建立了以色列这个独立的国家。但就在建国次日，周边的五个阿拉伯国家连手入侵，不让它生存。当时这五国有四千万人口，以色列只有60万，刚宣布建国，还没有正规的军队，危在旦夕。但以色列人不是想“有没有可能”，而是想建国“应不应该”。最后选择坚守自己的家园，绝不屈服，全民奋战，终于打败入侵者，保住了这个新兴的国家。 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;近年获得独立的东帝汶也是坚持了西式的“应不应该”而成功。东帝汶才80万人，不到印尼两亿人口的0.5％。在苏哈托军事政权专制三十多年中，如果按照“可不可能”的思维，那独立就连想都别想。但最后东帝汶人成功了，因为他们的领袖和人民，坚持的是理念原则，而不是绞尽脑汁琢磨操作上的可能性。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;科索沃也是这样，面积才一万多平方公里（原为塞尔维亚的一个自治省），只有200万人口，不到塞尔维亚的五分之一，但科索沃人坚持要独立，经过多年的奋斗，最后也梦想成真，成为一个独立国家，至上月底，已获得国际上62个国家的外交承认。他们初期的艰难，同样令人感到“不可能”。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;联合国刚成立时，全球才50多个国家，现已近200个，等于增加了四倍，说明独立、分离、民族自决，是世界潮流。关键是看这个民族的决心，尤其是其领袖对国家前途的认识是否清晰和坚定。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;跟流氓论理，是君子的错 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;今天，任何谋求跟中共要民主、要真正自治，都是与虎谋皮，完全没有可能！至于提什么中国的宪法精神，即使作为策略，也是不会有结果的，因为共产党本身都从来不按自己的宪法行事；宪法是花瓶，你以为那里能长出鲜花，那是你的错。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;今天西藏流亡政府应该做的，是认清现实，尊重真实；这个现实就是，中共政权无论如何都不会给藏人真正的自治，这是专制者的本性决定的。不管你怎么让步、怎么妥协、怎么磕头、怎么恳求，全都没有用！举一个再典型不过的例子：达赖喇嘛本人的确是发自内心要放弃独立，他千百次地在西方清楚地表达这种想法，并通过他的代表直接无数次地告诉北京。但中共硬是把“藏独”这顶帽子“钉”在达赖喇嘛头上，然后用其绝对控制的媒体强力宣传，于是十三亿中国人一致认定达赖喇嘛是“藏独”的罪魁祸首，对他痛恨万分。面对中共这种流氓，你要拿它当君子一样去论个理，不是中共的错，是你的错。人家清楚地按独裁者的原则、理论行事；而你原则没有，理念不清，要斗过人家，怎么有可能？<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;那面对如此“强大”的中共独裁政权，西藏流亡政府现在怎么办？应该旗帜鲜明地提出来，西藏历史上是个独立的国家，现在被中共占领了；必须结束共产党在西藏以至整个中国的专制统治，联合所有人，推动中国民主。未来中国民主了，六百万西藏人民用公投决定自己的前途。如果那时（民主）中国能够尊重藏人的选择权，平等对待西藏民族，那么达赖喇嘛可以提出，希望西藏留在中国，实行高度自治（只能是建议，不能是决定！）以达赖喇嘛在藏人中的崇高声望，多数藏人很可能会听从。如果不是这样，那就必须尊重全体西藏人民的选择。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;传播西藏的“两个真实” <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;今年达赖喇嘛已74岁。他不仅是西藏历史上十四位达赖喇嘛中最长寿者之一（有九位达赖喇嘛寿命不到50岁），更是走向世界，在全球最受欢迎的精神领袖之一。既然过去二十年来致力跟中共谈判的“中间道路”毫无成效（今后也不会有成效，这是必然的），就应该改变策略，把重点放在，向世界传播西藏的两个真实：西藏历史上是独立国家的真实，多数西藏人民要求独立的真实。由此积攒国际上了解真实的人们支持的力量，同时也促使更多西藏境内的藏人觉醒。把希望放在自己的人民的觉醒上，而不是寄托到中国统治者的开恩上。或者说，不要寄望于统治者的变化，而是依靠人民知道真实后产生的力量。<br />
&nbsp; &nbsp;现在的中间道路明显是走不通的，而向世界传播西藏的两个真实，也不是可以立即见效的。既然都不能获得明显效果，那为什么不走真实的道路？而且，只有走真实的道路，才有可能获得真正的结果。一味谋求跟北京谈判的妥协政策，只能自我麻痹，涣散斗志，在给中共专制统治的合法性“背书”的情况下，误导西藏内部的藏人。导致他们也不了解自己的国家的历史，不敢想像独立的可能，还会产生“沉默螺旋”效应，使他们认为，无论是境内还是海外的藏人，多数都不想独立，只有他自己一个人心里想，那就干脆放弃吧。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;西藏人民的觉醒是关键 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;至于说强调西藏属于中国，才能获得中国知识分子的支持，那也是本末倒置。因为西藏问题的根本解决，关键是看六百万西藏人民的觉醒程度和抗争的力度。从以色列复国，东帝汶独立，科索沃建国，更不要说当年美国脱离英国的独立战争等等，全部的例子，都是靠本民族的努力，而不是依赖殖民统治者内部的知识分子觉醒。对方民族的知识分子发出良知的声音是宝贵的，但不是关键，更不是“前提条件”。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;以达赖喇嘛在当今世界的崇高威望，在全球范围内传播关于西藏的“两个真实”，是绝佳的机会。一旦这位第十四世达赖喇嘛圆寂，如果找到“转世”，还要至少近二十年之后，才会有一个成年的“达赖喇嘛”；更不要说，要达到现在这位达赖喇嘛的全球性声望，岂止是不容易，甚至是不可能。因为现在这位达赖喇嘛具有独特的个人魅力：他的气质、风格、为人的谦恭、对他人关照的细心、孩子气的率真，坦荡的开怀大笑，以及他讲话时特有的劲头等等，都是独特的，无法模仿的。这种世界性声望和爱戴，绝不是将来哪个人叫“达赖喇嘛”就可以达到的。如此可贵的“个人资源”用在“中庸之道”上，实为一大遗憾。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;藏人心底的呼唤是“独立” <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;我丝毫不怀疑，在绝大多数西藏人的心中，独立是他们发自心底的梦想。只是由于面对中国恶龙的霸气和暴政，他们不敢发出真正的声音；由于对精神领袖达赖喇嘛的巨大尊敬和爱戴，他们以宗教情怀推崇达赖喇嘛的政策。但自由是每一个人心底的渴望，挣脱强权的压迫、建立自己的家园，是古往今来全世界每一个民族都一路浴血奋战所争取的，西藏人民绝不会例外！<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;达赖喇嘛和流亡海外的藏人，如果转向重视多数藏人内心呼唤的“西藏独立”，传播这个真实、这个现实，让世界更清楚这个真实，尤其让每一个藏人知道他并不孤独，让每一个藏人心中的梦想都继续燃烧，这种梦想的燃烧，就是为恢复一个独立的西藏国铺路！无论道路多么艰难，无论需要多少代人的努力，但这是一条迟早必定要走的路！既然如此，为什么不从脚下开始？！无论这开始的一步多么稚嫩，在漫长的复国道路上显得多么微不足道，但它是通向心底呼呼的道路，通向梦想的道路，是正确的道路！在迷失的道路上跑一万米，不如在通向梦想的正确道路上挪一寸。无论任何人，为实现自己梦想所作的每一丝努力，都有潜在的巨大力量：因为它真实。人类古往今来的一切成就、一切胜利，都是追求梦想的结果！西藏人的梦想，也迟早必定会成为人类梦想成真的一部分！<br />
