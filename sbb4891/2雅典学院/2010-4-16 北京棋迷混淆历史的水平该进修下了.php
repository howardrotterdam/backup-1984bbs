													<h2>北京棋迷混淆历史的水平该进修下了</h2>
						
						
																											<div id="postmessage_566019" class="t_msgfont">当然，混淆的内容，不是他的原文，是他署名，也就是说，是北京棋迷背书的<br />
<a href="https://1984bbs.com/viewthread.php?tid=43191&amp;extra=page%3D1" target="_blank">https://1984bbs.com/viewthread.php?tid=43191&amp;extra=page%3D1</a><br />
”原来波兰历史上就是地主资产阶级的的反共急先锋。就在这个卡廷惨案发生的十多年前，法国英国出钱，波兰德国出兵，要“把红色政权扼杀在摇篮里”，不但攻进苏联，占领大片国土，还抓了几十万苏联红军战俘。。。。。。“<br />
<br />
这个段话我们可以一句一句分析<br />
<br />
”历史上是地主资产阶级的反共急先锋“，这个词本身就是一个”阶级仇“的表述，而且彻底是共产国际的话语，根本没有价值，而且，就当时本身叙述来说，”波兰的爱国者“也包含在这个”急先锋“之内的，引文见下。<br />
<br />
再看卡廷前十多年的苏波战争：”波兰德国出兵“，搞笑了，发生苏波战争的时候，已经是1919年了，那时德国早就被解除武装，怎么会德国波兰一起出兵那？而且，早在苏波战争前，列宁就跟德皇签订了臭名昭著的《布列斯特合约》，俩国家从那时候就开始穿一条裤子，一直穿到了希特勒进攻苏联。<br />
<br />
同时，还要指出，发动苏波战争的，一方面是波兰和俄罗斯两个民族历史上的世仇，1795年沙俄、奥凶和普鲁士就把波兰瓜分干净了，一战的时候，根本波兰这个国家，1916年，德国帝国政府，也就是普鲁士的合法继承，答应波兰复国，一战后，确切说，是十月革命做掉沙皇和临时政府后，而奥凶也消亡之后，当初瓜分波兰的三个国家（三个障碍）都不存在了，这样，波兰才有立国的可能。<br />
<br />
而发生苏波战争最根本的理念，是列宁和托洛茨基”不断革命“理论，也就是后来作为托派被批判的”一国不能建成社会主义理论“，按照那个理论，俄国的共党革命，只是引发西欧工人暴动的导火索，只有通过俄国――波兰――德国的革命输出路线，才可能在全欧洲建立共产帝国。也正是因为这个关系，<br />
<br />
如果对上述叙述有异议，可以去参考俄共布九大的文献<br />
<a href="http://www.xj71.com/mlqjfile/200905080617552337.doc" target="_blank">http://www.xj71.com/mlqjfile/200905080617552337.doc</a><br />
（我前面提到地主阶级和爱国者，也在这个文件里有）<br />
<br />
另外，至于说“不但攻进苏联”。这里也有一个常识错误。苏联那个时候还没建立，那个时候是“苏俄”。这并不是简单的咬文嚼字，苏联的领土比苏俄大多了。苏联直接就是沙皇帝国的继承，而苏俄仅仅是沙俄里俄罗斯那部分，不包括沙皇帝国的其他部分。波兰当时进攻的基辅、明斯克，也就是今天的乌克兰白俄罗斯，一直到1922年跟苏俄拼凑成了“苏联”，这些地方的所谓苏维埃政权，同样是红军的刺刀下建立的。因此波兰对苏俄的进攻，不仅仅是协约国针对苏俄与德国媾和的讨伐，更是波兰立国，与原来的宗主沙俄及其后续，在领土冲突上必然会发生的战争。<br />
<br />
把论述建立在这样的史实上，想要有价值，真的很难。至于那篇文章里通篇的”报复“云云，这种话拿出来，跟直接号召中国人去东京强奸屠杀一样的，素质如此，不必多说了。即使波兰发生过对苏联的屠杀，也不足以成为苏联大规模脑后开枪的理由――更何况那个传说中的屠杀，至今只在共产分子自己都没完全相信的传说中。<br />
