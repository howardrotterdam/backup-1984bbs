													<h2>你爱的是哪门子国？</h2>
						
						
																											<div id="postmessage_759591" class="t_msgfont">最近经常有人转载、引用一些名人名言痛批爱国主义：<br />
<br />
&nbsp; &nbsp; <strong><font color="red">国家是为人而建立，而人不是为国家而生存。 ――爱因斯坦</font></strong><br />
<br />
&nbsp; &nbsp; 爱国主义是无赖最后的避难所。 ――塞缪尔・约翰逊<br />
<br />
&nbsp; &nbsp; 爱国主义就是积极地为了微不足道的原因杀人并被杀。 ――勃特兰罗素<br />
<br />
&nbsp; &nbsp; 除非你把爱国主义从人类中驱逐出去，否则你将永远不会拥有一个宁静的世界。爱国主义是一种有害的、精神错乱的白痴形式。爱国主义就是让你确信这个国家比所有其他的国家都要出色，只因为你生在@e。 ――乔治・肖伯纳<br />
<br />
&nbsp; &nbsp; 说这些话的都是国际级的重量级的名人，各国都有，可见爱国主义在人类中是普遍存在的一种情感，跨种族跨文化，并不只有我们中国人对爱国主义特别的情有独钟。美国人也很爱国，到处挂国旗，昨天学校校庆放焰火，当第一发焰火升空的一刹那，我旁边的美国学生就开始高呼“USA ！ USA ！USA！”好不鸡冻。最近很火的Joe Wong也在他的笑话里拿&quot;USA is No.1&quot;来调侃美国人的这种爱国情结。德国日本就更不用说了，整天大和、大日本的叫着，德国国歌第一句就是&quot;Deutschland Deutschland über alles,über alles in der Welt.”翻译成中文就是“德国德国胜过一切，胜过世间的一切。”你看看，多他妈狂，要不两次世界大战是怎么干起来的？<br />
<br />
&nbsp; &nbsp; 爱国主义其实跟宗教狂热差不多，具有很强的排他性而不具有普适性，伊斯兰教说安拉是唯一真神，基督教说上帝才是唯一真神，这两种说法最起码有一种是错的，很有可能是全错，肯定不能全对就是了。同样的，亚美尼亚的爱国主义也许是一种美德，阿塞拜疆的爱国主义也可能是一种美德，但是它们不可能都是美德，我更倾向于认为，亚美尼亚的爱国主义和阿塞拜疆的爱国主义都不是美德。这两个巴掌大的国家因为一块芝麻大的地方打了20年仗，也没打出个所以然来，我亲眼目睹这两国总统在奥运会期间争风吃醋，简直可笑之极。我想，一个高贵的灵魂是由智慧、勇气、坚韧、仁慈这些要素来决定的，而不是他出生在哪个国家。正所谓“英雄不问出处。”<br />
<br />
&nbsp; &nbsp; 既然爱国主义有着非理性和反智的本质为什么还能大行其道呢？因为人本来就不是纯理性的存在嘛，作为一种群居生物，人会恐惧、会有不安全感、无力感、害怕孤独和寂寞，喜欢依赖，喜欢虚张声势，喜欢优越感。这样，当一个人加入到一个群体当中的时候，他的心灵得到了抚慰，身份得到了认同，力量得到了延伸！他会本能的觉得很舒服，会觉得自己变强大了:你看我们伙儿多牛逼，我属于我们伙儿，所以我也很牛逼！本质上，宗教狂热和爱国主义拥有同一个源头，就是人性的弱点。一个精神强大、人格独立的人可能笃信宗教，但这完全是出于对未知的敬畏和宽容，所以不会狂热，而爱国主义他是碰都不会碰的！<br />
<br />
&nbsp; &nbsp; 爱国主义虽然不理性，但是有他存在的土壤――人性的弱点，这跟我们为什么不能消灭犯罪是一个道理，存在即合理，这个理不是理性思辨，而是客观条件。但是我们不能因为消灭不了犯罪就去鼓励犯罪。欧美日人也是人，这些国家当然也有爱国主义，就跟这些国家也有犯罪是一个道理。<br />
<br />
&nbsp; &nbsp; 爱国主义如果再细分的话主要有三个基础，价值认同、民族认同、文化认同，当然了这种认同是建立在优越性的基础之上的，一个人不可能因为一个价值很荒谬而去认同它，更不能因为一个民族很低劣而去认同它：你看我这个民族跟蟑螂一样，我是蟑螂我自豪！这种人纯有病。世界各国的爱国主义也就是这三种外加一种，我们逐个举例看看：<br />
<br />
&nbsp; &nbsp; 英美爱国主义：价值认同为主<br />
<br />
&nbsp; &nbsp; 价值认同型爱国主义是最偏向理性的爱国主义，是最好的爱国主义。这个国不是代表了一片具象的土地或者一个具体的民族，而是一种抽象的理念和价值，美英的爱国主义就是这样一种爱国主义，英国是第一个君主立宪制国家，美国是民主世界的老大，两国文化一脉相承，美国又有所发扬，对个体的尊重，私产保护，自由贸易，民主政治，权力制衡，小政府，人权，法治，自由这些理念深入人心不可动摇，所有这些构成了英美价值的基础。这种爱国主义门槛也最低，你认同这些价值你就可以爱国，无论你是什么肤色、来自何处。所以全世界各个地方都有人怀揣美国梦来到美国，追寻这些价值，实现自我。你看现在美国大兵跟爷似的，当初独立战争的时候也特别艰苦，都没有鞋穿，大冬天的走一路一路血。人家靠的就是这种爱国主义――“无代表，不交税！”华盛顿将军是英格兰人的后裔，但是却带领美国人民打击英军，去他妈的同文同种，你剥夺我的权利就是不好使！华盛顿心中的国不是哪个民族，而是理想和信念！即便与同胞为敌也要坚持正义！<br />
<br />
本来源自英国的绅士概念也在全球范围内构成了男人的标杆，据说泰坦尼克号的船长史密斯的临终遗言就是“Be British!&quot;在当时的情况下，这句话肯定不是说要你做一个盎格鲁撒克逊族人，而是要求大家尽量体现出绅士风度，把生的希望留给妇孺。&quot;Be British&quot;短短两个单词就可以完美诠释英美爱国主义的精髓，我所信仰的就是最好的，你们也该信仰！<br />
<br />
&nbsp; &nbsp;德日爱国主义：民族认同为主<br />
<br />
&nbsp; &nbsp;这种爱国主义是最危险的爱国主义，德国跟日本这两个国家虽然相隔万里，但是却惺惺相惜，有着惊人的相似之处，这两国人都十分勤勉、精于制造、一丝不苟、近代的历史也颇为相似，都是后发资本主义国家，德国1871年才统一，日本1868年才开始维新，之前都是被其他列强狠揍那伙儿的，拿破仑就说过这样的话“我要扫除神圣罗马帝国（德意志民族的松散联合体）这个大粪堆！”日本1853年遇到坚船利炮之后也是签了一大堆不平等条约。屈辱啊，自卑啊！后来真的就是因为这两个民族内在的优秀品质，人家咸鱼翻身了！分别一跃成为资本主义第二强国和亚洲第一强国，跟那种凤凰男一样，处境改变之后自信心爆棚，说白了就是土包子开花：“我原来一穷二白被人欺负，靠我自己努力才有今天，我也能欺负人了，我容易吗我？我最优秀！” 后来纷纷找老大装逼被灭，老实了。但是给世界带来了很多灾难。话说回来，德国和日本的爱国主义也是有基础的，人家的国真的值得爱，因为这两个民族取得的成就确实是世界上绝大多数民族取得不了的，被灭之后，时至今日仍然是世界第二三强国，从这里来找优越感也是合情合理。尤其是德国，出的哲学家科学家和音乐家不胜枚举。<br />
<br />
&nbsp; &nbsp; 法意爱国主义：文化认同为主<br />
<br />
&nbsp; &nbsp; 这种爱国主义是最装逼的爱国主义。我认识的法国人不少，那叫一个优越啊，张口就说我们法国菜世界第一，商量的余地都没有。他们的优越来自对本国文化的自信。同样的还有意大利。这两个国家一定程度上奠定了当今西方思想文化的基础，法国人可以自豪的说，没有俺的启蒙思想你们是个屁啊！意大利人笑了，没有俺的古罗马和文艺复兴你也甭扯启蒙思想。不只是思想，吃喝拉撒睡，衣食住用行也是文化，这些方面法国意大利更是引领世界潮流，你要是在纽约时尚圈混你敢不会说法语？你在东京装装逼不也得背个LV嘛。辜鸿铭显摆学识还要嘲笑别人拉丁语不过关！你要是学美术，学建筑，学设计，你敢不去巴黎和罗马？时装周也是巴黎和米兰最重要，纽约伦敦更富，白扯。就是吃个饭，要是正式点的也得吃法国菜或者意大利菜，没听说谁跟女朋友头一回约会去啃德国大肘子的。法意生活方式就是牛，美国人也追求，法拉利，兰博基尼，玛莎拉蒂，这才叫速度与激情，你开个美人豹试试？所以都德才大言不惭的说“法语是世界上最美丽的语言。”他连一门外语都不会，但人就有这个胆儿！我的文化最优秀，你们都得仰慕!虽说这属于装逼吧，起码人家还是有一点资本的。<br />
<br />
&nbsp; &nbsp; 上述国家的爱国主义都包含了价值认同、民族认同、文化认同这三种基础，只不过侧重点不同，如果列举这些国家改变世界的名人，直接省略5000字。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;还有一种爱国主义特别的盛行于两个东亚国家！<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;中韩爱国主义：意淫为主！<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;前面说了，无论是那种爱国主义，都是建立在优越感的基础之上的，但是如果确实没什么优越的怎么办？成熟一点的国家就是不办。如果一个国家国民的心智停留在人类发展的初级阶段而且又好面子呢 ? 好办，编！近现代的人类文明成果是基本没我们的份儿了，一切都有据可考，不好忽悠，我想起来我们数分老师杨小远几乎每堂课必说的“中国对世界科技的贡献连1%都不到”而她也是援引路甬祥的话。怎么办呢？于是阿Q在这一刻灵魂附体，正如在每一刻一样！“我的祖上也阔过！”过去的事儿你们都没经历过，好骗！<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;1连拉带扯外加不识数的上下五千年！<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;我们总说我们的文明史是五千年，也就是说我们的文明可以追溯到公元前3000年左右。在考古学的领域里，文明（civilization）和文化（culture）是两个概念。文明是比文化要高级的，是后出现的，对于文明出现的判定标准，主要是城市（人群聚集）的出现，文字的产生，国家制度的建立。此外还有金属冶炼等等。据现有的证据来看，我国的第一个文明是殷商文明，可以追溯到公园前1500年左右，这是国际上都承认的，甲骨文，青铜器，殷墟，证据确凿，一样不少。于是我们有了3500年文明史，之前的什么仰韶啊，红山啊，河姆渡啊都是文化。然后看看周围，发现比我们早的文明还有一大堆，古埃及（前3500年），古巴比伦（前3000年），古印度（前3000年），爱琴海（前3000年）…………这可不妙。于是我们想到了一本叫做《史记》的文言文穿越神话历史小说，发现里面还有个夏朝，据推算是公元前2000年左右，可惜相关考古证据一样没找到，文字也没有，青铜器也没有，城市也没有，于是洋人就不承认了。而且很奇怪的是，甲骨文上一点前朝的事情都没有记载！不管！《史记》上说有就有！你们说没有就是侮辱我们中华民族！<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;再往前实在是连毛儿都找不找了，于是我们就有了这个结论：2000+2000=5000， 这就是上下五千年的由来！<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;2王婆卖瓜的“四大文明古国”<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;梁启超说的，国外也没人承认，不知道这个四大是怎么来的。按年头算？排不上。按人口算？无从统计。文明程度？别人家都是砖房，我们是茅草屋。有人说了，其他文明都灭了，不能算，就我们一直存在！ 于是我想起了苏格拉底，修昔底德，希波克拉底，柏拉图，亚里士多德，阿基米德，我想起了在全世界随处可见的科林斯石柱，爱奥尼亚石柱，多利克石柱，上海就有很多，我的家乡长春也是有的，还有山花。我想起了阿尔法，贝塔，伽马………………，我想起了金字塔，空中花园，汉莫拉比法典。不知道在希腊某处会不会有人一下子想到盘庚，妇好，武丁之类的人来，以及甲骨文。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;于是这个四六不靠谱的假说在我们这成了常识。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;3其实挺小的“四大发明”<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;既然在发明前面加了大字，就说明这四样很重要，我们一向认为我们发明的这四样东西改变了全世界，可悲的是这种说法竟然是一个英国业余科技史爱好李约瑟者提出来的，他37岁才开始学习汉语，最初接触到中国文化是因为跟一个中国女留学生发生了婚外情，此人50年代作证还被证明是一个不诚实的人。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;先不批人家的人品和学识，就事论事。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;首先要说，近现代的造纸术，火药，指南针，和印刷术，完全与中国的发明无关，我们没有任何证据证明欧洲人学习了我们。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;造纸、火药、活字印刷术、指南针，都是较为原始和简陋，除造纸外，根本没有实用价值。以毕升的泥活字印刷术为例，只有几个人试用过，因不实用而放弃，所以也没有流传下来，连实物都没有流传下来，木活字印刷术印过少量的书，但实践证明泥和木活字沾水，是容易变型的，所以直到清代，中国都是用雕版印刷术，直至从外国传进金属活字印刷术。国际上现在公认韩国清州兴德寺最早使用金属活字（铜活字）。其铜活字印本《佛祖直指心体要节》（1377）比德国古登堡铅印版的《圣经》还早70年，被联合国教科文组织认定为世界最古老的金属活字印本。被指定为联合国教科文组织世界纪录遗产。作为最早的金属活字印书，韩国虽比德国古登堡早七十年，但世界还是公认活字印刷术的发明者是德国古登堡，因为他的铅活字印刷术技术最先进和完善，所以各国后来一直用他的铅活字印刷术至今。中国的阿Q说世界的造纸、火药、活字印刷术、指南针技术，是从中国流传去的，但这说法并没有证据。西方的造纸、火药、活字印刷术、指南针，制作所用材料配方和工艺技术同中国不同，还有中国的阿Q说中国的发明在前，西方是受到中国的启发，以活字印刷术为例，古希腊古罗马就有很多金属印章文字，可见创造活字印刷术的灵感不可能来自中国。中国历史上的造纸、火药、活字印刷术、指南针，因为原始和简陋，根本不能称之为“发明”，而中国现在所用的造纸、火药、活字印刷术、指南针技术，倒是来自西方完善先进的发明。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;比如黑火药吧，炸药奖那个炸药咱先不提，连能够实用的黑火药都是从欧洲传到中国的，最早是明代的弗朗机，葡萄牙来的，之前我们的火药配比不对，真的就是“火药”能着火，但是没什么爆炸力。我们还说最早发明了火箭其实这个“火箭”真的只是字面上的“火箭（fire arrow）”而不是火箭(rocket)，类似的东西任何古文明中都找得到。<br />
&nbsp; &nbsp;&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;指南针最逗，大家伙都知道那个八卦盘子上的勺子吧，是一个“砖家”根据古书上的十二个字“复原”出来的，“司南之杓，投之于地，其柢指南”，不要说出土文物了，就是连张简笔画一样的图片都没有！<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 造纸术确实不错，好歹是大规模应用了。可惜质量比不过纸莎草和羊皮纸，很快就烂没了，所以外国有3000年前的手稿，数量不少，我们要考证就得靠读骨头上或者大鼎上的字了，再不就是小竹片。而且用刷子（brush）写字，纸张再好，直到近代还是文盲大国！既不有利于文献保存，客观上也没起到文化大规模传播的作用。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;4孔子，各种子<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;孔圣人是最有名的中国人，以至于老外编一句无厘头名人名言都喜欢煞有介事的加上“子曰，Confucius he say”（注意是 he say 不加s 表尊敬），其实根本不是孔子说的，是他自己编的俏皮话。还有人专门整理这些屁话，比如，Confucius he say：Crowded elevator always smell different to midget.或者Confucius he say：Man who walk in middle of road get run over by bus.其实就是老外对于孔子的调侃，看似严肃，其实都是屁话。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;黑格尔对于孔子和以孔子为代表的儒家思想是很蔑视的，他在《哲学史讲演录》中说到孔子时，指出孔子没有“思辨哲学”，《论语》中只有一些“常识道德，这种常识道德我们在哪里都找得到，在哪一个民族里都找得到，可能还要更好些，这是毫无出色之点的东西”。因而不值得一顾。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;子在川上，曰：‘逝者如斯夫!不舍昼夜。’<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;孔子在河边说：“时光的流逝就像这河水呀，白天黑夜都不停息！”<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;这就是一句废话！跟“下定决心，不怕牺牲，排除万难，去争取胜利”一样废！<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;其实吧，孔子处于人类文明的初级阶段，如果把人类文明比作一个人的人生的话，孔子那时候相当于两岁，孔子的思想相当于说了一句“妈妈，我要吃饭饭”，当时来看“我靠，这孩子都会用叠词了！”真了不起！<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 我们现在混到了成年时代，于是时不时的想起了“妈妈，我要吃饭饭”这句里程碑一样的宣言，时不时派砖家去解读一番！还他妈能上电视！这让我们时时刻刻陶醉在“妈妈，我要吃饭饭”这样富有哲理的深刻思想中！<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 孔子的思想强调“仁爱”，不过似乎古今中外没有人强调“不要仁爱”的。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 孔子的政治理想是恢复周礼，什么是周礼？就是王吃饭面前要摆九口锅八个碗，公爵八口锅七个碗，侯爵七口锅六个碗……………………直到一口锅没有碗。你累不累啊你？<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 这就是我们这个民族的最杰出文化代表，伟大的思想家，我们不但专门发明了衍圣公这个头衔，就因为他是孔子的后代就供着，还到处修庙纪念。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 西方文明的小萝莉在孔子那时候都能说“2+2=4”了，也没看着柏拉图的后代被供起来！<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 后头就更没人了，连说个话都要“代圣人立言”，不然就不能说话，相当于你他妈都小学五年级了还成天“吃饭饭”！<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 除了孔子之外，最能代表中国国人的就是李小龙和姚明成龙之类的了，如果不算大喇嘛的话。<br />
 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 其实要论发明创造和发现，王淦昌是最有希望让中国人自豪的，他差点发现了中子，最后不巧阴差阳错被查的威克这小子捡了漏。不然我们就可以自豪的说“我们中国人发现了中子！”<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 不过王淦昌上的是清华留美预备校和柏林大学，他的成就还是归功于西方的教育体系，他能够证明我们人种智力上不比白人差，这几乎在西方已经是常识了，不需要证明。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 就算我们喜欢延长自己的历史，我们能办世界上最昂贵的奥运会，世界上最昂贵的世博会，仍然不能弥补文明发展水平的巨大差距，我们骗不了自己的内心。被深深的自卑感所支配，在西方人面前抬不起头来，只能把“强大的祖国”当成唯一的精神支柱，只有在心里默念着“我是‘中国人’的一员，我背后是强大的‘中国’”给自己打气，才勉强敢于与西方人的目光对视。所以，他们无论如何也要竭力维护这个“强大”的、足以充当他们畸形自尊的资本的“祖国”。至于这个“强大”祖国是多少亿屁民被当作活燃料烧掉才发动起来的，不关他们的事。在中国流行着这样一个urban legend，说是美国护照上有这样一句话，大意是“无论你身在哪里，请记得强大的美利坚合众国永远是你的坚强后盾”云云。现在想起来，这么不靠谱的故事之所以在中国流传了这么多年，被一众人津津乐道，无非又是那种喜欢狗仗人势的奴性发作而已。自己缺乏自信，非得凭借着“隶属于某一强大群体”这样的念头才能找到力量感，因此在他们的想象中美国人、西方人也是一样；中国人自己看人下菜碟，对他人的态度完全取决于对方的国籍或社会阶层出身，对来自穷国或下层的人是一百个瞧不起，因此在西方被白人看轻，也自然而然地马上想到对方的态度是冲着他们的国籍去的，尽管实际上白人看轻的可能是他们的自私自利，可能是他们对待社会那种事不关己的冷漠态度，可能是他们穷人乍富、一来西方马上跑到奢侈品店去疯狂扫货的土豹子相，可能是他们身上典型东方式的狡诈、虚伪、小算计，甚至可能正是他们那种愚民嘴脸。 实际上，被不被洗脑倒在其次，关键这种同声同气、同仇敌忾的气氛，令他们产生了一种深深的归属感，使他们错误地觉得似乎自己是“中华”这个强大力量的一员，中国的“强大”就是他们的强大、中国的霸道就是他们的霸道、GCD打着中华民族的旗号以压倒性优势将一切反抗力量镇压得体无完肤，让他们与有荣焉――尽管在GCD眼里，他们连奴才都算不上，只不过是一头头待宰的肉猪。而GCD也很善于利用中国人的喜欢狗仗人势的这个特性，时不时地通过宣传机器制造出几个“中华民族的敌人”给他们骂着玩，耍得他们团团转，令他们心甘情愿地在凌虐虚幻“敌人”的癫狂快感中走向他们自己的屠场。 <br />
 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;网上有许多这样的帖子，《XX没什么好牛逼的》，XX可以换成27个省名，内容其实都很搞笑，比如，孔子是他们省的。或者一个杀了3000万人的屠夫是他们省的。抑或刚才那个屠夫的水晶棺的水晶是他们省出的。不信你们问度娘，都搜得到。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;病态的民族性格其实是由一个个病态的人格组成的，你出生的地方不一定就是美丽的，也可能很贫瘠很恶劣，你的民族不一定就是伟大的，也可能就是跟蟑螂和老鼠一样，你能做的就是自己尽量不去做蟑螂和老鼠，而不是试图证明蟑螂和老鼠就是比人强。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;爱国主义跟自恋一样，并不是说洋人的爱国主义就好，一样该骂！只不过恰巧人家的民族是奥黛丽赫本而已，你的民族是凤姐，恰巧。<br />
