													<h2>关于唯物主义，哲学普及一下</h2>
						
						
																											<div id="postmessage_548546" class="t_msgfont">　　很多人都觉得：虽然很多东西都不靠谱，但是唯物主义还是对的。这个想法是错的。<br />
　　在公认的著名哲学家的人中，主张唯物主义基本上一个没有*，仅仅这一点，就可以清楚地提示大家：唯物主义有多么不靠谱。<br />
　　至于唯物主义为什么不靠谱，说起来也简单，就是“物质”这个定义无意义。物质的定义是什么，唯物主义者会告诉你，物质的定义是“存在”。这样一来，唯物主义的基本命题“世界是物质的”，也就等同于“世界是存在的”。如果你再问一句：“世界为什么是存在的”，那答案则更简单：“因为不存在的东西就不在这个世界上了”。完全是同义重复，毫无意义。唯物主义者搞了这么一通同意重复以后，就以为对世界有了认识，沾沾自喜起来。<br />
　　当然，话说到这里，有人可能问了：“世界是物质的（唯物主义）”是同义重复，那么“世界是精神的（唯心主义）”不也一样吗？<br />
<br />
　　事情其实是这个样子的：<br />
　　首先，很多哲学家尤其是现代哲学家，并不愿意纠缠于“唯物唯心”争议，拿“唯物”“唯心”归类的，主要是辩证唯物主义者，他们的逻辑是：非我族类，即为唯心。因此，我们不能用“唯心主义同样不对”来替唯物主义辩护，哲学并非只有这两种观点。<br />
　　其次，当然也有一些“真正的”唯心主义哲学家，此外，马克思也不是傻瓜（虽然马克思主义者大多数都是傻瓜），为什么有哲学家要当唯心主义者，而马克思又偏偏要当唯物主义者呢？这个就要细说一下了。<br />
　　唯物主义：materialism，唯心主义：idealism，这两个词本义中并没有“唯”这个字所表达的那么强烈的意思，此外，唯物主义有“物质至上”的附加含义，而唯心主义有“理想主义”的附加含义。可想而知，理想主义比较符合哲学这个学科的面貌，加之很多哲学家研究的是类似“心学”的内容，他们自称为唯心主义就很自然。唯心主义并无“世界一定是精神的”之含义，而主要指的是研究问题的对象和方法。但由于“心学”这类内容被大多数人认为太玄幻，近现代的很多哲学家不接受唯心主义也顺理成章。<br />
　　那么，马克思又非傻瓜，他为什么要搞唯物主义呢？这首先要从“历史唯物主义”来入手。马克思关于生产力决定生产关系的论述，就很有“物质至上”的意味。当然，在历史问题上搞“物质至上”，并不意味一定要全面搞唯物主义，毕竟马克思主义历史观和“世界是物质的”并无必然联系。为什么马克思非要搞唯物主义呢？这种不合逻辑的地方源自犹太-基督教的特性，马克思是犹太拉比世家出身**，更加深受一神教的毒害。犹太人的上帝，就是为了某种利益（姑且不论是物质利益，还是心理利益）而被捏造出来的，并无任何理性基础，而是直接加以设定的。唯物主义者的“物质”定义和“世界是物质的”，就完全是套用的这种逻辑而产生的：既然人类心理上需要一个上帝，那么我们就塑造一个上帝；既然我要在社会历史中搞物质决定意识，我就编造一个唯物主义哲学。<br />
<br />
　　好吧，分析一下唯物主义的害处。<br />
　　首先，唯物主义看起来很符合事实，问题是哲学不是一个“看起来怎么样就怎么样”的学科，哲学是专门较真的地方。如果非要根据“看起来怎么样就怎么样”的逻辑行事，那么哲学看起来就很没用，这个学科早该取消了。<br />
　　其次，“世界是物质的”一说空洞无物却让人以为获得了知识，这本身就有阻碍人们进一步获得真正知识的嫌疑，这一点在辩证唯物主义中有更直接的体现***。当然，仅仅唯物主义而不加辩证法，仅仅“世界是物质的”这么一句话，倒也不至于真有多大的作用。<br />
　　最后，唯物主义更大的坏处，在于如果人类过分看重一个空洞的概念（物质），就难免会为它赋予一些想当然的内容。举一个直观的例子：<br />
　　你开车走到半路，有人打电话告诉你：“你前方500米处的路上有个东西，由于某种光学效应，你在汽车上可能看不大清楚。”如果你相信这个人，会怎么样呢？是不是要减速慢行，以免直接撞上去。<br />
　　应该如此。但是这并不一定是个好选择，因为对方只告诉你“有个东西”，万一这个东西的属性是：“径直开过去没事，只要速度低于某一个值，靠近就会爆炸”呢？<br />
　　当我们认定有一个“物质（东西）”的时候，就会很自然地把一些最常见的属性套在它头上，而作为日常生活中的行车安全考虑，也确实不能把心思用在防范那么奇怪的例外上。但是搞科学研究，或干其它一些远离日常经验的事的时候，所面对的对象往往就不能“以常理论之”了。这也是为什么有这么多唯物主义者反对“相对论”，反对“量子力学中的测不准原理”或反对“量子化学中的共振理论”的原因。<br />
<br />
<br />
*：马克思在西方也是有影响力的，但他是作为社会学家而非哲学家而著名的。<br />
**：马克思父母都是犹太人，不过他小的时候，全家就改信基督教了。<br />
***：辩证法就是阴阳五行理论的一个西方变种，只配“指导”中医这类“科学”。</div>

							
							
							
							
