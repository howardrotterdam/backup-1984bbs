唯心主义的正确性，源于这样一个理念：“世界是一个梦”这句话是无从驳斥的。如果世界是一个梦，或者更直观地说，就像《黑客帝国》里那样的虚拟现实，那么我们所见的一切物质，都是依托于精神而存在的。（当然，你可以诡辩说：精神还是要依托于某种物质啊！但这个物质没意义，因为这个物质的任何规律都可能和你现在知道的物质规律完全不一样。因为你现在知道的关于物质的一切描述，很可能都是“主宰”编出来哄你玩的，你现在的“物质”定义很可能完全不对应实物）<br />
