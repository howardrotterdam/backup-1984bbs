													<h2>冉云飞：成都市公安局阻扰我到港过程纪实</h2>
						
						
																											<div id="postmessage_267990" class="t_msgfont"><a href="http://www.bullogger.com/blogs/ranyunfei/archives/343188.aspx" target="_blank">http://www.bullogger.com/blogs/ranyunfei/archives/343188.aspx</a><br />
<br />
<br />
早在八月前，香港大学新闻及传媒研究中心的朋友们即策划一个叫“新媒体时代的公民话语力量”的学术研讨会，邀请我参加，让我谈博客的生存与公民社会。同时邀请我参加该中心编辑出版，由翟明磊兄撰写的《中国猛博》一书的发行讨论。我忝列明磊所写的猛博之列，因此去与会会朋友们的心情异常迫切。再加上此一活动在十一大假期间，我的一些亲人也在港，我也就算公私兼顾，乐得探亲与公干并行不悖。<br />
<br />
 <br />
<br />
有了这样的打算，于是我就去港澳通行证――因为以前的通行证过期了――特选一个有三个月可多次往返的探亲证。在办探亲证的同时，我也办以一个十年期的私人护照，这一切都非常顺利，中间并无滞碍。但当我9月22日拿到通行证和护照的时候，敝单位人事部领导电话通知我说，请明天到单位来一趟，领导想找你谈话。我当时心里就想，公安真是明察秋毫，监视工作做得不坏。第二天到单位到会议室，看到市、区国保、公安（辖区警察）、单位领导，一并九人，已端坐如仪。此前我也有多次与他们打交道的经验，其中有很多人是熟人，但我与朋友们有点不同，没有写过他们，也没有写喝茶的经历，以至于有一些朋友看不到我喝茶的文字，觉得不可思议。一来我不想这些事，这些喝茶的经历也没有什么了不起，二来我不想成为事件的主角。<br />
<br />
 <br />
<br />
作为主发言人黄国保一上来就说，今天我们到你单位来，不像我们上次喝茶那样属于轻松的聊天，这是非常正式而严肃的事情。国庆的时候，你不能到港，这是非常时期。这是上面的意思。我当然问哪个“上面”，他们当然不说。我说，我尊重并遵守法律，公民有出行的自由。我需要你们拿出理由，他们说你也清楚。我说我并不清楚，他们说这些事你心照不宣。后来大家谈开才知道，他们过国庆，却不让我到港去参加学术去避国庆，真是可笑。我这个一贯不跟制度中的个人结梁子，不跟哪一个个人结仇，这是我的一个原则。我想他们是执行者，底层警察也不易。我说，那就这样吧，我退后一步，既然你们国庆的时候害怕一个公民到港，那我也可以考虑放弃国庆的时候到港参加一个叫“在中国”座谈会。同时，我说要去参加港大四至号的学术座谈会，同时兼探望我年迈的岳父母和可爱的小侄女。于公于私，我想都说得通。你们不是怕十一敏感吗？那我十月三号过去吧，怎么样？他们也说我们把你的意见向上反应，我说那好吧，我等着你们的回复。<br />
<br />
 <br />
<br />
第二天，我为了让他们更多地了解港大学术会议的事情，我亲自主动到派出所，将邀请复印交予属地派出所的曾所长，请他将此事和内容转达给国保。他承认传达上去，同时我还给黄国保打了电话，他也回了话，说我们把你的想法传达上去。29日，愚人兄从美国回来，好不容易带着病躯到我家中来看望我，我没谈上几句就很抱歉的说，因为派出所已约好让我去谈一谈，所以我只好抱歉地告知愚人兄，只有下次回来再谈。好在现在有网络，不然我对愚人兄的歉疚会更加难以释然。我去派出所，曾所长问我国庆期间有什么活动，并问我是不是常参加星期二大慈寺茶会。我说我并不常参加大慈寺茶聚，十一没什么活动，十一上午会到李氯斯示恿怦胶炔枇奶臁Ｎ颐挥惺裁纯梢瞒的，我还告他我买了三号到深圳的票，去参加那个港大学术会议。昨天我们一家人去参加了这个朋友聚会，其中有沙河先生夫妇等，有许多老朋友，原来大部分都是大慈寺的茶客，这样才反过来证明大慈寺的雅聚也在官方的注意之列，官方之草木皆兵若此，复夫何言？<br />
<br />
 <br />
<br />
中午吃饭时我接到派出所张警官的电话，说晚间派出所所长要见见我，我说可以。晚间莫之许、王大联、马小鹏、卢刚诸兄约了我在如是庵街懒鬼火锅喝酒，我于是告知警方在此处来谈。老莫此前一直说，你肯定去了不了香港，我相信我的判断。我认为避开十月一日应该可前往，我说那么多和我一样的朋友都去了，一点事都没有。老莫说，你不懂，你有一点不同。我说，我有何不同？老莫说，你签了零八宪章，而且是首批。我说这个也有关系么？老莫说，这关系大了。后来警方来此处约上我，带我去我单位的会议室，果然气氛严肃，市局一国保和锦江国保大队侯队长在座，还有敝单位党组吕书记。侯队我们曾喝茶，谈得也较也轻松，但这回简短地来告知我，经上面研究，说我不能去香港。我说，我需要理由，需要法律条款。他们说，没有，就是不能去。我说我已经做出了退让，你们还不让我去，这太过份。侯队说，这回没有条件可讲，也不是讲条件的时候，同时你的事情我们也还在继续调查。我说你调查没有结果之前，我总是没有被剥夺权利的公民吧。侯队继续说，这回真的没条件可讲。我说我真要走呢，侯队说你要走也走不了，有边控，你走不了。然后不欢而散。我当然不甘心不给理由，即限制一个公民的行动自由，自然心情郁闷。<br />
<br />
 <br />
<br />
本来放弃与北风、长平、梁文道、翟明磊、张铁志诸兄的会面，就令我不爽和痛苦，因为丧失了一次与朋友们喝酒聊天请益的机会。我放弃这次机会，我还给主办的杜婷及朋友们打招呼，暂时不要把我不能与会的实情告知出去，因为不愿意给搅扰他们的国庆。同时我也放弃了一些媒体对我的采访，我想我这样退一步，先做妥协的姿态，应该有所回报。看来他们认为你的退让、妥协、合作都没有什么价值――虽然我以后会继续如此做――傲慢、冷漠、无情、无理、违法是这个政权的一贯做派。我一个小小的公民，当然不敢且也不能把这冷硬无理的政权怎么样，我只想说，无论你同不同意我的见解，希望大家都学会点妥协学会遵守宪法，才是一个社会的福音。我只能说老莫确实久历江湖，他说成都国保肯定不让你到香港去，我想我还是个天真和乐观的人，对权力机器的运营及其没有定准、翻云覆雨，认识不够，看来以后要多向老莫请教。<br />
<br />
 <br />
<br />
我重申我的看法，自由出行是一个公民的权利，何况在你们看来普天同庆的国庆呢？你既然觉得人民对国庆都欢欣鼓舞，那么你为什么惧怕一个公民自由出行到港去参加一个学术会议呢？我并不想为难谁，也不想与谁结梁子，我批评政府与制度，这都是我作为一个公民应有的权利。这权利受宪法保护，不应该被违宪剥夺，同时也不应该因此受到任何干扰与惩处。我如实将上述过程写出来，不是为了为难谁，也不是为了公安机关，我认为这反而是使沟通更加透明公开的方式。与此同时，也算是保存一个时代的史实，让人们知道争取自由的艰难。其实专制制度下的不自由，也让国保、公安同受捆绑。我为他们节假日来阻止我这样一次正常的出行，而不能与家人过节休息，感到难过和遗憾。<br />
<br />
