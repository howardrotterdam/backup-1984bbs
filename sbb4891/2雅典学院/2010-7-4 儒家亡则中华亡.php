																											<div id="postmessage_730301" class="t_msgfont">识心与本心略说<br />
<br />
意识心，简称识心。本心，即良知心，佛教名之为菩提心、真如心等。亦称本性、自性等。在中华文化中，心与性往往通用、同义，就人身而言为心，就生命而言为性。<br />
<br />
识心与本心是不一而亦不异的关系。识心为用，本心为体；识心为末，本心为本；识心为器，本心为道；识心为人，本心为天；识心为现象，本心为本质；识心为形而下，本心为形而上；识心为本心之所生，本心为识心之所以生。<br />
<br />
识心与本心关系密切，故不异；又有一定区别，故不一。识心可以断灭，本心不断；识心属于“假有”，本心真有；识心变化不常，本心真常；识心是因缘所生，本心非因缘；识心是生灭法，有生有灭，本心非生灭法。（良知是生生不灭，真如是不生不灭，这是良知与真如的最大区别，或者说是儒佛两家对本心解悟的最大不同处。）<br />
<br />
另外，识心有善有恶，本心知善知恶而又超越善恶，那是一种超越性的至善，不能以人世的善恶概念去衡量和“范围”它。<br />
<br />
于本心的真常真在，儒佛两家认知无异。佛教认为现象界的一切都是无常苦空无我的，唯独真如佛性具有常乐我净四德。佛经有时也说真如佛性“不常”，那是提醒世人，对真如的“常在”不能用普通的、现象的“常”去理解。《中论》说“不常亦不断”，须知“不断”就是“常”啊。<br />
<br />
一切现象都是时间内的存在，唯独本心有超越性的一面，即在乎时空又超乎时空，时间空间都是它所的变现和作用。关于本心之“常”，《大般涅磐经》有一段经文讲得很透彻：<br />
<br />
“凡夫之人戏论诤讼，不解如来微密藏故。若说于苦，愚人便谓身是无常，说一切苦，复不能知身有乐性。若说无常者，凡夫之人计一切身皆是无常，譬如瓦坏。有智之人应当分别，不应尽言一切无常。何以故？我身即有佛性种子。若说无我，凡夫当谓一切佛法悉无有我，智者应当分别无我，假名不实，如是知已，不应生疑。若言如来秘藏空寂，凡夫闻之生断灭见，有智之人应当分别如来是常，无有变易。若言解脱，喻如幻化，凡夫当谓得解脱者即是磨灭，有智之人应当分别，人中师子虽有去来，常住无变。”<br />
<br />
经文中“无有变易”“常住无变”的“如来微密藏”、“佛性种子”，就是指的本心。这才是真正的无价宝藏，永永远远取之不尽用之不竭。于人类社会而言，古今中外一切智慧的创造、文明的成果都是它的开发显化。而且呀，人的肉身与识心也不过是它生生不息、新新不已的“产物”而已。<br />
<br />
对于本心，信之解之行之证之，大不易。善意善念善意识以及由此引发的善行，乃是致良知、证真如的必要条件，但不是唯一的。大善还要大智，百尺竿头还必须更进一步，借助相当的智慧，才有可能解悟本心的奥秘。<br />
<br />
