													<h2>郭德纲事件 有关政治的方面和非政治的方面</h2>
						
						
																											<div id="postmessage_777335" class="t_msgfont">郭德纲事件，有关政治的方面和非政治的方面<br />
刘松萝<br />
<br />
近来，郭德纲新闻不断。先是有媒体报道郭德纲私占公共绿地，接下来依次是郭德纲的弟子李鹤彪殴打采访记者，郭德纲出面支持打人者并且谩骂记者、穷人和“中国人”，打人的李鹤彪被行政拘留，以及德云社自行停业学习自查等等。最新的进展，是传闻中的郭德纲被封杀，德云社的演出执照被吊销。<br />
<br />
郭德纲事件并不复杂，但由于牵涉到的问题比较多，又被人为地复杂化了，所以不好评论。对这一事件的反响，集中地表现出国人思维方式的缺陷：没有范畴，没有逻辑，把什么东西都一锅烩。<br />
<br />
对于郭德纲事件，我的主要观点是：假如郭德纲和德云社被封杀，我支持郭德纲。同时，我反对李鹤彪殴打采访记者，反对郭德纲藐视基本的社会规范。<br />
<br />
在郭德纲事件中，有关系到政治的方面和非政治的方面，需要分开讨论。<br />
<br />
1. 关系到政治的方面<br />
<br />
德云社是一个文化演出团体。政府对文化演出团体的管理，应该依照法律而不是官员的好恶。即使依照现行的法律和制度来管理，也应该留有余地，因为在现行的法律和制度中对公民表达自由的考虑尚不够充分。假如因为近来发生的事情就不准郭德纲和德云社演出，是侵犯公民的权利。除此之外，我们还应该考虑对公民文化权利的尊重。政治权力，最好不要介入到文化特别是民间文化之中。<br />
<br />
我也反对郭德纲的弟子李鹤彪殴打记者。北京电视台的记者不是在挖明星的隐私，他们采访的是一个有关侵占绿地的公共事件。如果我看到的视频还算完整，那么记者们并没有执意闯入郭德纲的住宅。李鹤彪打人之前，他还在和记者谈论绿地问题和北京电视台在报道中存在的问题。就在没有发生争吵，而且记者还在解释先前报道中可能出现的偏差时，李鹤彪动手了。<br />
<br />
不论是浙江省遂昌县警方通缉记者，还是德云社的李鹤彪殴打记者，都是应该谴责的。有人会说，你把二者相提并论，是混淆视听。那好，我首先强烈谴责遂昌县警方通缉记者，然后再谴责李鹤彪殴打记者。我还要说的是，没有成大气候的人就敢打记者或者支持打记者，要是得了势，还不直接把记者给杀了？<br />
<br />
有人认为北京电视台招人厌恶，记者有行为不当之处，所以该打。那么，假如查一查，被通缉的记者也许不是无懈可击的，是不是应该抓起来呢？<br />
<br />
其实，郭德纲们和有些官员一样，认为记者是可以打，可以抓的。<br />
<br />
2. 关于非政治的方面<br />
<br />
郭德纲的相声不是庸俗，是民俗。相声的价值，就在于民间性。马季和姜昆的那些配合政治任务的相声，才是真正的庸俗。不仅政治把相声变庸俗了，而且相声也把政治变庸俗了。郭德纲保留和继承了很多传统的相声段子，恢复了相声的本来面目，是有很大贡献的。即使是在相声界里郭德纲的反对者中间，有些人后来也重视了传统相声的段子和手法。相声回归小剧场，回归民间，回归民俗，是一条正确的道路。<br />
<br />
与此同时，郭德纲也有很重的江湖习气。自闻达以来，不断地惹是生非。成功以后，本应该心平气和，宽厚待人。郭德纲反其道而行之，闹出了有关同行和师傅的诽谤案。从郭德纲的相声功底来看，他的师傅也应该是民间的高人。即使郭德纲没有从师傅那里学到什么，也没有必要反咬一口。他的师傅也不厚道，执意要走刑事起诉的途径。郭德纲代言“藏秘排油”，本来就是一件不妥当的事情。出现问题之后，他还在相声段子里面现挂，把“藏秘排油”当做儿戏。由此可见，德云社留不住能人就不奇怪了。郭德纲对待员工的方式，以及经营德云社的方式，只能够作为反面教材。<br />
<br />
绿地事件，本来可以心平气和地处理。就算其中有阴谋，郭德纲也不可以深陷阴谋论之中，用斗争的方式解决问题。至于后来把记者说成妓女，把邻居说成穷人，打人“打就打了”，打人者是“民族英雄”，甚至在打人者道歉之后还要在深夜发文大放厥词，实在是无法理解，只能解释为疯狂了。<br />
<br />
3.简述：维护社会规范是一种客观需要<br />
<br />
有些话尽管不受欢迎，还是应该说出来。<br />
<br />
郭德纲最近的行为不仅完全没有道理，完全没有必要，而且是在向基本的社会规范挑战。对于这样的人，应该让他付出代价。在一个正常的社会里，他会受到舆论的谴责，受到观众的抵制。而在现今的中国，郭德纲却被一些人看做是大英雄。<br />
<br />
再说一遍，我不赞同用行政手段处理郭德纲事件，特别是牵连到德云社。我还想指出，假如听任演艺界的名人胡作非为，在公众之中希望对他们加以抑制的要求就会增加。此时，来自官方的打压虽然并不适当，却也会成为民意的一种体现。<br />
<br />
很长时间了，自由主义者们选择的典范实在是拿不出手。昨天是范美忠和《色&#8226;戒》，今天又是郭德纲，越发等而下之。<br />
<br />
自由主义者们，不要迫使人民在自由和民意之间做出选择，在自由和基本的道义之间做出选择。<br />
<br />
2010.8.10.</div>

							
							
							
							
