																											<div id="postmessage_453961" class="t_msgfont">沙叶新按：一些网友来电和来信，问我前不久发在我博客上的《天下兴亡谁有责？》一文为何被屏蔽了，我想这是最近有关方面为了大力建立“河蟹”社会，而特别加强网络控制所致。该文发布之后，短短时间，点击数万，评论数百，海内外众多网站纷纷转贴，反响良好，影响甚巨。屏蔽实在可惜，沉默于心不忍，只得“改头换面”，再贴一次，看看我尊敬的《博客中国》能否高抬贵手，得以放行。谢谢了！2010、1、23<br />
<br />
*******************************************************************************<br />
<br />
国家事管他娘！<br />
<br />
<br />
在学校，老师念：“天下兴亡，匹夫有责。”问一同学什么意思。该同学以为“天下兴亡”是“天下‘姓王’”，便脱口答道：“王是大姓，老师你姓王，我也姓王，我们班很多同学都姓王，是姓王的天下，所以天下姓王。”老师目瞪口呆，又问：“那匹夫有责呢？”他以为是“‘皮肤’有责”，便又答道：“天下姓王，皮肤怎么能不负责呢？老师你想呀，只有我们中国人才有姓王的，中国人是黄种人，是黄皮肤。外国没姓王的，是白种人，是白皮肤，所以天下姓王，皮肤是有责任的。”老师哭笑不得，对他说：“唉，你呀，不假思索！”他还以为老师赞扬他，越说越来劲：“谢谢，我一点不假，我是真思索。”老师昏倒！<br />
<br />
“天下兴亡谁有责？”这在中国是关乎爱国主义的大问题，而中国人一向被认为、也自认为是最最爱国的；不爱国者，是汉奸，是国贼；十恶不赦，狗彘不如。中国长期以来，尤其是近60年，也是世界上对国民进行爱国主义教育最广泛、最持久、最不厌其烦、最大张旗鼓的国家；从幼儿园，到敬老院；从中央电视台黄金频道的节目，到乡村小茅房破败墙壁的标语；从少先队的“五爱”教育，到全社会的“五讲四美三热爱”的宣传运动，无一不有神圣的“爱国”的内容。<br />
<br />
但“国”是什么？是“祖国”？是“国家”？是“政府”？是“政党”？是“政客”？是“领袖”？……教育者从未向被教育者说清楚，也不让大家清楚，就胡里胡涂地被代表了。更别说“国”是什么性质的国了：是民国？是王国？是极权国？是共和国？……也含糊其词，也一律被代表。<br />
<br />
王同学将“天下兴亡”误为“天下姓王”，他把崇高的爱国名言，像是德里达似的后现代地给解构了。这倒也是歪打正着，可以让每一个单纯、善良的爱国者，在热烈地爱之前先冷静地问一问，问问这个国姓什么，问问这个天下究竟是谁的天下。如若“天下姓王”之“王”是帝王之“王”，那么“兴，百姓苦；亡，百姓苦。”匹夫（平民百姓）总是受苦，凭什么要为帝王的天下承担兴亡之责？凭什么要爱“普天之下”的“莫非王土”？<br />
<br />
在我过往的70年中，在我生活的国之中，经历过两次极为惨烈的战争，一次是在我童年，交战双方是中日；一次是在我少年，交战双方是国共。前一次为抵抗日本的侵略，是抗日。后一次是国共两党的争霸，是内战。两次战争并非都与“天下兴亡”有关，“匹夫”并非都应“有责”。<br />
<br />
抗日战争攸关国家之命运，失败了，中国就要“亡国灭种”。这乃大是大非，事关民族存亡，所以那时传遍全国大地的《义勇军进行曲》高唱：“中华民族到了最危险的时候，每个人被迫发出最后的吼声！”所以当年震动校园内外的《毕业歌》呼唤着：“同学们大家起来，要担负起天下的兴亡！”这才是真正的“天下兴亡，匹夫有责！”<br />
<br />
国共之间是内战，是两党争“王”，再怎么打，打翻了天，至多是一个党兴，一个党亡；至多是政权的交替，王朝的更迭。这不是“天下兴亡”，只是“改朝换姓”：是姓“国”的为王，还是姓“共”的为王。<br />
<br />
国民党的“国”叫“中华民国”，其实并非“民国”，它不姓“民”。当时流行的说法是“蒋家天下陈家党”，信奉的是一个领袖，一个政党，一个主义。正因为它专制独裁，抗战胜利之后，便冲昏头脑，发动内战，更加上严重的吏治腐败，终于在1949年输给G产D，龟缩在台湾这蕞尔孤岛，但仍称中华民国，其实还是“党国”，并非“民国”。只是等到蒋经国去世前，废除了党禁，还政于人民，又经过岛内的蓝绿两党的较量和全民的普选，台湾的民主之花才逐渐开放结果，如今倒很有点“民国”的样子了。<br />
<br />
上一世纪，G产D创立之后的10年，也有个国，它是在共产国际扶持下，于1931年11月7日（和前苏联的国庆同一天）建立的，叫做中华苏维埃共和国，首都在江西瑞金，M泽D是苏维埃主席，副主席是项英、张国焘。这个国，当然姓“苏”，是外国姓；这个“国”也确实拿过外国银子，卢布。可惜国祚短暂，终结于1937年9月6日。<br />
<br />
后来被M泽D感谢过的日本的侵略，在当时确确实实帮了G产D的大忙，使得G产D能以“七分发展、二分观望、一分抗日”的“持阄战”，赌徒似的和“日寇、蒋帮”持阄一赌，从而在“日蒋相争”中“渔翁得利”，发展和壮大了自己，得以东山再起；日后并以“人民民主”为旗帜，以“为人民打江山”作号召，打败了抗战八年而元气大伤的国民党，终于“枪杆子里出政权”，建立起“中华人民共和国”。<br />
<br />
1949年建政伊始，G产D不乏开明作风，颇有民主气象，纲领也能共同，政治也能协商，政府也能联合，选举也能照章，所作所为，也都像模像样。<br />
<br />
可是马上得天下，并非马上就能治天下，到后来，一直到如今，已一个甲子，G产D恐怕是越来越难以逃脱历代王朝的兴亡周期率：“其兴也勃焉，其亡也忽焉”；如今已很不像立国之初的那个样儿了，不是“人民共和”，而是“一党专政”了。所有的民主党派早就有了“跨党”的中共“地下党”；文革期间，甚至公开地干脆从“地下”走到“地上”，使得“民主党派”完全可以倒着念了，成了“派党主民”（派G产党主宰民主党派）。到如今，尤有甚者，任何机关单位都有党的组织，都有党的领导，无远弗届，无孔不入；不但民主党派，甚至连宗教团体、佛门净土也难以幸免！国民党独裁时期，尚不能控制所有媒体和所有学校，而今在G产D的天下，所有报纸都形同党报，所有学校都类似党校，连军队也成了比希特勒的“党卫军”还忠诚于党的“卫党军”了！<br />
<br />
这样的国家究竟是谁的天下？早在1957年储安平就说过这是“党天下”，他说了实话，却被打成右派，以后生死不知，至今也下落不明。如今半个多世纪过去了，有7千多万党员的G产D成了“天下的第一大党“，有960万平方公里的中国也成了世界“第一大的党天下”。<br />
<br />
这样的党天下，这样的国家，匹夫能负其责吗？匹夫首先要有权，才能有责。“兴”时匹夫无权，“亡”时才想到匹夫有责；不把匹夫当主人，只当炮灰，谁干？除非“裸”干。<br />
<br />
须知国家和祖国，内涵虽有交叉，但是区别甚大。<br />
<br />
国家不同于祖国。国家即政府，政府即国家。国家有庞大的机构，有法院、有军队，有监狱，有警察，还有很多政客和官僚，也就是当官的。这些当官的必须是经过民主程序由全体公民直接选举出来的，才是合法的。否则你再怎么解释，再怎么辩解，再怎么给自己涂脂抹粉，都是非法的。由这些政客、官僚，当官的所组成的政府当然也是非法的。这样的国家有个名称，就是极权国家。<br />
<br />
需知，祖国并不同于国家，更不是极权国家：祖国是生我养我之地，是我家族繁衍之所，<br />
<br />
是我先人入土之家；祖国是黄皮肤，是乌眼珠，是黑头发；祖国是东海潮，是黄河浪，是长江峡；祖国是甲骨文、是毛笔字、是水墨画；祖国是中餐菜，是茅台酒，是龙井茶；祖国是《梁祝》的传说，是《西厢》的剧情，是《西游》的神话；祖国是春节，是端午，是中秋；是兰草，是牡丹，是梅花；祖国是轩辕帝，是孔夫子，是普普通通、千千万万的你我他。<br />
<br />
我无条件的爱我的祖国，但我只能有条件的爱我的国家。我绝不可能不爱我的祖国，但我很有可能不爱我的国家。<br />
<br />
祖国不是国务院，不是裆中央；祖国不是八宝山，不是纪念堂；祖国不是共青团，不是太子党；祖国不是最近报废的“周济”导弹，祖国不是深不可测的一片“汪洋”；祖国不是三军官兵的阅兵方阵，祖国不是四代领袖的巨幅画像；祖国不是大裤衩，不是水立方；祖国不是漂着死鱼的湖泊，不是埋葬矿工的煤窑，不是压死汶川无数学生的“豆腐墙”。<br />
<br />
祖国不会强拆你的祖屋，国家会推倒你的房梁；祖国不会窃听你电话，国家会阻止你上网；祖国不会拒绝你申诉，国家会对你强行截访；祖国不会抢夺你的耕地，国家会巧取你的钱囊。祖国不会禁止你出入境，国家会为你制造“柏林墙”。祖国不会指控你有泄露机密、危害安全罪，国家会控告你颠覆政府、反对裆中央。<br />
<br />
唉……我多么想爱我的国家噢。我也曾真心实意地爱过，爱得发狂发飙；我何止是“爱国贼”，简直就是一个“爱国盗”。可如今要我再说一声爱，就怕言不由衷，理不直气不壮了；虽然我还是想说，只盼着我可以由衷地大说一声、十声、百声、千声、万声“我爱你”的那天来到。可如今，真的抱歉了，你已不那么神圣，不那么崇高。正如开国之君M泽D的教导，你只是秦始皇和马克思的杂交。起先还可以，“一唱雄鸡天下白”，“时间开始了”，心潮逐浪高；可是，越到后来越糟糕，时间停滞了，甚至倒退了，今天你已是由金钱铸造的利益集团所组成的现代王朝。请问，你为何至今不敢公布官员们的财产，不敢晒晒衙内们的生财之道？再问，60周年的超豪华国庆，究竟花了多少钞票？那是党的钱吗？不，都是民脂民膏！切莫给整齐划一的方阵迷惑了，切莫给流光溢彩的烟花忽悠了，那只是为了扬威，只是为了炫耀，只是为了掩饰空前的虚弱和万分的焦燥。那些龙吟虎啸，那些男欢女笑，说穿了，只是紧闭门窗的卡拉OK，只是刺刀护卫的假面舞蹈。“盛世”，为何紧张得对行人盘查搜包；“和谐”，为何恐惧得禁止商店卖刀。何不干脆再下禁令一道：全国人民都不许笑，因为笑里也会藏刀。你还要我为你骄傲，还说你的名字比我生命更重要。不，我真的要含泪相告，60年过去了，你何时有过反省？何时下过罪己诏？最后，尊一声我的国，60年来，为什么你总与良民为敌，总拿好人开刀？以往的李九莲、林昭，眼前的许志勇、师涛……还有那胡佳和刘君呀，都是那么有爱心的好人一个呀，他们或被打被压，或被关被抓，或被宰被杀，他们究竟触犯了你王法的哪一条！<br />
<br />
至今我还没选择放逐和流亡，今后也不永别家乡，这实在是我无比热爱我的祖国，也实在是我对这个国家还心存一点希望。可我要说，如此下去，国将不国，这样的天下，我怎能负责它的兴亡！<br />
<br />
2009、11、2上海善作剧楼</div>

							
							
							
							
