<br />
如果在有神论视角下，共的思维显然是不值一驳的。如果切换到无神论怎么办？<br />
<br />
比如说：康德告诉我们上帝存在无法证明同时无法证伪。我们应该假定他存在，称之为先验的。在有神论视角下，我们可以保持着和上帝一对一的契约，我们可以相信自由和法制来自上帝的预设。如果是无神论怎么办？共的存在明确告诉我们：世界上是没有伦理道德的，没有王法的，正义是无法声张的，一切都是靠暴力的。在一个神不存在的世界上，共完全可以大肆玩他的那一套辩证法。“任何事情都没有对错，有没有法制区别不大嘛……自由都是相对的……一切都是相对的……” 法治精神的根基扎在哪里？价值体系的根基在哪里？我之前觉得中国的儒家是可以支撑法制精神的，但是儒家对个人主义是有排斥的，并且儒家自己也早已名存实亡了。<br />
<br />
举个例子：基督徒吴维尊文革时因为拒绝读毛主席语录被下狱。他说：所有人都是一寿有疆的，怎么可能单单毛一个人万寿无疆的呢？我为什么要说违背神意的胡话呢？<br />
<br />
抛开他个人不说，一个无神论者怎样才能说出这样的话？无神论的自由主义者怎么才能判断这种精神价值？怎么解释这种自寻死路毫无价值的死？当然你可以说尊严，但在没有尊严的地方，你的尊严不值一钱。<br />
<br />
说到底，无神论的自由主义能不能避免向功利化，甚至是犬儒主义滑坡？马克思韦伯说过资本主义精神和新教伦理的密切关系，是不是无神论视角下驳斥共思维是无解的？</div>

							
							
							
							
