　　力挺小沈阳，惹来与专家口角。有人捧，有人恨。切齿者有之，眼红者有之。其实，大可不必。我挺小沈阳，是艺人间的义气。论艺人成名，无非一命二运三风水，四积阴功五读书。步步血泪，声声吟叹，待行至峰顶，又高处不胜寒了。成名后，一大三大，名气大了，开销大是非大烦恼大。岂能尽如人意，但求无愧于心。艺人的苦恼，是外人无法想象的。您看到的是舞台上的花团锦簇，景片后的凄凉又有何人知晓呢？&nbsp; &nbsp;<br />
<br />
　　我尊敬那些在自己领域钻研的专家教授，那是真正的学者！为国为民，用血汗做学问，这些位是我心中的圣人。我痛恨的是那些披着教授马甲的假专家。正文一点没有，闲白比谁都强。踢寡妇门，刨绝户坟，骂聋子，卷哑巴，调戏老太太，打小孩，一天到晚跟浪风抽得似的。看谁红骂谁，老憋着跟人家分账。人家一还嘴，他还犯小性，跟自己怄气，假模假式的装流氓。真流氓我都不怕，何况你这模仿秀呢？读圣贤书，做仁义事，您真不懂吗？百鸽呼雏，乌鸦反哺占其仁，蜂见花不采结尔群，鹿见灵芝不逮鸣其众，占其义，羊羔跪乳，马不欺母占其礼，蜘蛛罗网而食，蝼蚁塞穴避水占其智，鸡非晓日不鸣，雁非暖日不至占其信。都做到教授了，连仁义礼智信都不懂，还需要说相声的来普及，不丢人吗？看起来，专家无情教授无义啊！那位老说别人泡沫文化的教授，您换换说法行吗？逮谁说谁泡沫文化，您得出新作品呐。按春晚的标准也得一年一个新的，您这都五年了，还更新不？呵呵，德薄而位尊，力小而任重，此万祸之源也！口口声声是学者，一天到晚在演艺圈里腻着，人前反三俗，被窝看。又想当教授，又想立牌坊。长虫绕葫芦，假充龙戏珠。估计这些位也确实没正事干，哭着喊着不炒作，可谁红往谁跟前凑合。这种心态，比结账后的嫖客空虚，比收工后的小姐寂寞，比年假里的鸡头孤单，我都想给教授点小费了。拜托，不要再用牙齿阉割艺人了，我们都穿上铁裤衩了，小心嘴。&nbsp; &nbsp;<br />
<br />
　　 去吧，干点正事吧，灾区重建，台湾回归，贫困儿童，西部缺水，这些大事您快去研究吧。教授回头金不换！可千万别说这些与你无关，合着您就跟说相声唱二人转的犯能耐梗呐？再送您们几句话共勉：晨鸡初报，昏鸦早噪，哪一个不在红尘里闹。路遥遥，水迢迢，利名人上长安道。今日少年明日老，山依旧，人不见了。可笑可笑。&nbsp; &nbsp;<br />
<br />
　　 叹罢多时，拿出一张祖德老师的照片，仔细端详着。一个学者，一个专家，一个真正的教授，一个德艺双馨的圣人。峥嵘峻拔高旷，顶天立地磅礴于天下。窗外，早春的冷月挂在天上，星星在颤抖。又偷望一眼祖德，真的令人柔肠寸断，犹豫彷徨悲伤，剪不断，理还乱。我端详着祖德的脸，那是一张摩擦系数极大的脸，使我身临其境的体会到那个久远的年代。整张脸极喜兴，一看就是喝了糖尿。真的奇怪，人脸怎能长得如此标致，细看竟然极象骨盆，造物主太神奇了。那一双眼睛，甚是有神，如栗子，底朝上，与膀胱相贴，尖朝下，前面贴耻骨联合，后面依直肠。英语叫prostate。祖德的嘴，豪爽通达，大笑时能直接看到裤衩上的花纹。那两个大牙，更是巧夺天工，我常天真的想，要是请祖德到后厨开启大桶番茄酱，那该何其壮观！就这张脸，气死画匠，难死木匠，鲁班来了都得哭，四面吊线，旋不出一个尜儿来。&nbsp; &nbsp;<br />
<br />
　　 祖德对工作太忘我了，要注意身体，你不属于自己，你的身体属于所有肇事司机。另外，出门时，一定要佩戴避雷针，防患于未然。真有一天，被雷劈后，又被车压烂，用勺子往盆里舀尸体时，我们会难过的。&nbsp; &nbsp;<br />
<br />
　　请允许我赞美祖德。啊，祖德，你是一个奇才！一个伟人！有人背后议论你，他们说你：娘娘宫抱一兔爷，没人样。点灯不亮炒菜不香，不是好油。吃铁丝尿大筐。比电线杆子多挂下水。高粱地种荞麦，杂种。蛤蟆秧子跟着甲鱼转，装王八孙子。黄浦江翻船，浪催的。山羊群里跑京巴，狗日的野杂种。不！不！我不要听了，怎么能容忍他们这么说祖德呢？祖德为中国的历史发展做了多大的贡献！祖德的舍己为人，你们不知道吗？祖德的离去，明朝才有了郑和，祖德的离去，清朝才有了安德海，才有了李莲英！让我们赞美祖德吧，让我们吃饭时，都先把第一杯酒泼给祖德，祖德，给我们托梦吧！&nbsp; &nbsp;<br />
<br />
　　相比较祖德，他的弟弟信达就有些不妥了。你没有照顾好祖德吗！我知道你习惯性肠炎，把扁桃腺拉出去了，但你的心还在吗？你为什么拿祖德开玩笑？江苏靖江插宋文化公司的刘总亲口说，看见你在**用品店买橡胶制品往头上戴，说搞化妆舞会，你演宋祖德。你什么意思吗？我很气愤，要为祖德打抱不平，今天不把你骂出绿屎来，算你没吃过韭菜。蛤蟆转长虫又托生个王八，三辈没眼眉的玩意。说话也不利落，老跟含着狗粮似的，你个花椒木。一个眼的判官，瞎鬼。木鱼改梆子，挨敲的货。癞蛤蟆插毛，你算飞禽还算走兽？撅屁股看天，有眼无珠。不怕你眼儿紧，我这有大棒槌。说句实在话，为了你，操不尽的心，还有你大爷。子曰：唯祖德与信达为难养也。&nbsp; &nbsp;<br />
<br />
　　 也送几句话共勉：杀尽江湖百万兵，腰间宝剑血带腥。&nbsp; &nbsp;<br />
<br />
　　 呆犬不识英雄汉，粤东犹自叫铮铮。&nbsp; &nbsp;<br />
<br />
　　 有朋友纳闷，此二人一姓宋一姓刘，为何亲兄弟？各位不知，这里有一位英雄母亲。其母善守寡，养五子。长子宋祖德，次子刘信达，三子庞小杨，四子萧雀，五子夏二。广东扫黄办曾为这位英雄母亲赠一匾：行行出状元。&nbsp; &nbsp;<br />
<br />
　　呵呵，今天太快乐了。&nbsp; &nbsp;<br />
<br />
　　看有人还嘴吗。&nbsp; &nbsp;<br />
<br />
　　我很期待。。。。。。。</div>

							
							
							
							
