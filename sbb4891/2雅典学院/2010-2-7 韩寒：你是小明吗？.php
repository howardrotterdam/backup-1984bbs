<br />
按照机密文件《五毛构成》里所言，50个写作高手，100个写作好手，500个外围写手构成一个五毛旅。这次涌现的五毛党们，经过我的判断，还是以“外围写手”为主，但是时不时会出现几个“写作好手”出来试水，至于“写作高手”，应该是在系统内有相应职务的，他们至今出手的还不多。当然，主要原因是过年忙着应酬收礼。<br />
<br />
 <br />
<br />
近期五毛党的泛滥也是我开通新浪微博的一个原因，一来是因为五毛党对于新兴事物的反应比较迟缓，相信注册和登录QQ对于很多&quot;写作好手&quot;来说已经是极限操作了，另外一方面，微薄没有匿名发言，他们普遍害怕注册。最重要的是，万一他们在微博阵地中表现突出，上头指示要巩固阵地，必须手机绑定时刻引导舆论，这对于他们来说是灭顶之灾，本来就只有一毛一条了，这下好，发个短信引导一下正好一毛钱，算上手机充电的电费，里外里还要亏损几厘钱。大家不要嘲笑他们，他们一毛钱就能卖身，一千块就能卖肾了，对于他们，几厘钱也是钱。他们才是真正的活在最底层，但是和统治阶级有着最高度统一思想的物种。<br />
<br />
对于五毛党，我有着不同的见解。首先，我认为我们要允许五毛党的存在，每个人都有权利雇佣别人为自己说话，每个被雇佣者都有权利在任何地方说话。你如果能够把小明打一顿，然后用从小明身上搜刮来的钱雇个人再把小明骂一顿，这也算是你的本事。任何一个政府，都有为他宣传的机构,这是情有可原的。但是五毛党完全是政府的败笔，我以前以为五毛党的出现是为了引导舆论，现在想来我错了，因为你不会因为看见一堆人围着在吃屎而挤进去吃一口。五毛党本是中高层麻痹讨好最高层的产物，但随着五毛党的横行，很多光辉正确的人士只要一开口，明明是免费的，结果全都变成了五毛党，这严重打击了他们的积极性。本来进酒店开个房间一夜情，完事了出门全世界都说你是鸡，这种打击不言而喻。你一旦设立了五毛党，除了国内外舆论影响不好以外，所有你原本的拥护者都有了五毛党的嫌疑。我为什么很少赞美政府，一来是因为我怕别人说我是五毛党，二来若批评无自由，则赞美无意义，三来我已经纳了税，人民纳的税被用于五毛党活动经费，也等于是我间接赞美了政府。<br />
<br />
最后我将去挑选一些言论，分辨一下哪个是外围写手写的，哪个是写作好手写的， 哪个是爱国青年写的，哪个是无知少女写的，哪个是不高兴写的，哪个是没头脑写的。但是如果我冤枉了你，你是义务的，那么真的没有办法。谁让你的主子认为，和你一样的见解能值一毛钱呢。</div>

							
							
							
							
