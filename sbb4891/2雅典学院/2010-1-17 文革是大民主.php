很遗憾柳叶眉对民主概念本身没有兴趣。我理解你的情绪，所以我完全可以容忍你的各种辱骂。但是我希望你能多看一些理论。在讨论一个东西是不是民主的时候，你总要先知道什么是民主吧？</div>

							
							
							
							
