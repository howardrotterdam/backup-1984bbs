																											<div id="postmessage_437249" class="t_msgfont">“大民主”的实质是反民主 <br />
<br />
总有一些学者认为，“文化大革命”中出现的“大民主”就是民主，把它作为民主的一种。然后，或由此引申出民主是不能搞的，一搞搞成“大民主”就麻烦了；或由此认为“大民主”是不好的民主，要搞民主则一定要避免。这些话语颇易起到混淆视听的作用。鉴于此，笔者认为，我们应该坚决地表明，“大民主”绝不是民主。&nbsp;&nbsp;<br />
<br />
民主之真义 <br />
<br />
　　什么是民主呢？民主其实指的就是政治民主，它包含以下四项基本规定： <br />
<br />
　　第一，民主赋予公民言论权、知情权、选举权、监督权和罢免权，同时保证这些权利的自由与平等。 <br />
<br />
　　第二，民主实行少数人服从多数人决定的原则，同时实行多数人又要保护少数人的原则。多数决定的效力只能约束、规范人的行为，而不能用来限制、妨碍人的思想自由，甚至剥夺人的财产和生命。允许少数人坚持、保留自己的观点和看法，并在法定范围内维护和实施他们自己的政治权利。 <br />
<br />
　　第三，民主既要实行直接民主的形式，同时又要实行间接民主的形式。通常做法是，由人民选举出特定的公职人员，来代替自己行使管理社会事务和政治事务的权利。为保证国家公职人员真正代表人民行使权利，提出采取普选制、任期制、分权制和监督制等。直接民主，就是由全体政治成员直接决定或管理社会公共事务。 <br />
<br />
　　第四，民主与法制密不可分，实行民主必须同时实施法制建设。法制是民主的重要保障：法制使民主的原则和规范法律化、制度化，从而赋予民主以最高权威性；法制规定了民主的具体内容和范围，使公民明确了自己享有的政治权利；法制规定了民主实施的程序、方式、方法，使民主政治具有了可操作性；对于违反民主原则的行为，法制将给予相应的惩罚，从而保障着民主的有效运作。 <br />
<br />
“大民主”完全背离民主 <br />
<br />
　　众所周知，“大民主”包含“四大自由”，即“大鸣、大放、大辩论、大字报”。“大民主”最初出现在1957年，“文革”中达到鼎盛，曾经写入1975年的宪法；“大民主”还包含群众可以自行成立组织，对认为有问题的人，进行批判、斗争、抄家、体罚等。文革”中盛行的“大民主”，不过是贴上民主的标签，实则搞的是非民主的一套。 <br />
<br />
　　第一，“大民主”没有真实地赋予公民言论权、知情权、选举权、监督权和罢免权。 <br />
<br />
　　“大民主”含有的“大鸣、大放、大辩论、大字报”，表面上似乎使公民享有了充分的言论权，但实际上，“四大自由”不是随时随地都可以运用的，而是由上层决定；“四大自由”不是允许有不同的政治观点，恰恰是为了达到高度的统一，统一到某种“正统”、“权威”的理解、主张和声音。因此，公民使用“四大自由”要冒着极大的政治风险，一旦被认定为“非正统”，就得背上罪与罚的沉重的十字架；“四大自由”可以不负责任地进行造谣、污蔑，而不必承担任何后果。 <br />
<br />
　　“大民主”也没有提供公民知情权，公民得到的只是“官样说法”和各种不胫而走的“小道消息”。“大民主”更没有提供给公民以选举权、监督权和罢免权。公民既没有真正的选举权利，也无法对权力机构和政治领导人进行监督，乃至可以撤换其中的不称职者。 <br />
<br />
　　第二，“大民主”既没有实行少数人服从多数人决定的原则，也没有对少数人进行保护。 <br />
<br />
　　“大民主”信奉“真理往往在少数人手里”，强调真理总是由少数人发现的，而大多数人并不能够理解，所以大多数人必须跟着少数人走，主张多数人应该服从于少数人；“大民主”根本不对少数人进行保护，多数人可以任意地剥夺少数人的基本权利，包括剥夺他的思想自由、财产和生命，正如现在大家公认的，“大民主”是对少数人实行暴政。 <br />
<br />
　　第三，“大民主”实行的是与直接民主、间接民主截然相反的另外两种形式。 <br />
<br />
　　一是“上层运动基层”的形式。即基层一切听命于上层的安排、决定，并且基层一定要对这样的安排、决定表现出坚决的支持和不折不扣的贯彻执行。 <br />
<br />
　　二是“领导发现群众”的形式。“大民主”虽然要求领导者要听取人民的声音，要到群众中搞调查研究，从中发现问题，提出解决问题的方案。但是，群众社会实践中创造的这个经验好而那个经验不好，这个做法值得推广而那个做法必须禁止，都得由领导裁决、判定。 <br />
<br />
　　第四，“大民主”完全离开了法律制度，抛弃法治，实行人治。 <br />
<br />
　　“大民主”公然地以“无法无天”为自豪，蔑视和践踏一切法律条文。正如所宣称的，“文革”是一场史无前例的“革命”、“造反”，对任何东西都要打他个人仰马翻、杀他个片甲不留，它本身已经完全脱离了国家法制正常运行的轨道。 <br />
<br />
　　由以上可知，“大民主”根本不是民主，而是反民主。 <br />
<br />
（《理论前沿》2009年第20期　许耀桐文）</div>

							
							
							
							
