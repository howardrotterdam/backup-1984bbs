&nbsp; &nbsp;&nbsp; &nbsp; 我的大学只念了两年多之后，就开始工作了。所以我见到钱校长的机会不多。准确来说，只有一面，我记得是我大一新生的开学典礼。记忆里是一位很和蔼又很有智慧的老人。虽然平时在学校里见不到校长，但是学校到处都有钱校长的题字，他的著作，他写下的校训等等。可以说，那几年里，感觉钱校长都一直在每一个学生的身边。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 刚刚突然得知了钱校长去世的消息，我公司里有四个员工，都是上海大学曾经的学生，一时间我们都很感慨。当然，我们没有痛心疾首或者痛哭流涕，那样太过做作或者虚伪，因为确实没有太多的接触，也谈不上多么深厚的感情。但是，此刻，真的能感受到一种失去了某种重要的东西的感觉，我想这种东西，就是钱校长留给我们和这个国家的，他的智慧，他的岁月，他的影响力，他对祖国和人民深厚的爱。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 我为我曾经是他的万千学子中的一个，而感到自豪。<br />
