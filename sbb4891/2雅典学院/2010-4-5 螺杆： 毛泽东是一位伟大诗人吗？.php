<br />
第三回 父子连台各得其所 <br />
<br />
诗云： <br />
<br />
天生一个神仙洞，无限风光在玉峰。 <br />
<br />
老绾专定神仙洞，劣儿只喜攀玉峰。 <br />
<br />
各取所需连床混，笑煞京都八旬翁。 <br />
<br />
话说王老绾娶了余娘名组恋他风情万种，余娘赏他家资殷厚，两下俱觉遂心 ，花烛之夜肉搏酣战，哪想老头儿误打误撞促住余娘“小明茎”猛耍，搔弄得余 娘心花怒放，汪洋而泄。老绾不过就那旺火炉螳里橇了几撬，竟将根坚硬铁实的 肉棍儿熔化了，从此，他俩燕尔新婚，如胶似膝，建空就干，老绾竟如回复壮年 ，额头皱痕渐少了些，余娘晚睡做起，渐觉烦闷，遂合老馆商量买了两个丫头， 一个唤做金儿，一个唤做银儿，他便摆出家主母架式，唬得两个小女子畏手畏脚 。 <br />
<br />
