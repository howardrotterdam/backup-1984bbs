王景一对眼珠儿随着乳头跃动，他见后娘淫得可以，心头似有烈火焚烧，胯 下小鸡鸣竟也硬挺横起．状若弱笋嫩鞭，红润白净，又似削皮紫荆藤，硬则硬矣 ，只是太小，王景扯下裤头，瞅瞅老父阳物，又瞅瞅自家小鸡，自家也觉相去甚 远，余娘戏道：“小儿甭急，来时方长，啊，乖乖，痒痒，帮我挠挠。” <br />
<br />
王景雀跃而至，急切相问：“挽哪？挠哪？” <br />
<br />
余娘正处十万火急当口．全身每处俱觉酥痒难止，尤以双乳为紧，他以手拍 拍自家圆物，目不能张，口不能言，王景求之不得，一手抓捏一个乳头，拧搓不 止，口里附渐乱语，揉了几搓，似觉不上劲，便以头抵余娘下巴，衔住顶端紫亮 圆柱一阵叮咬，又一阵狂吮，左左右右，忙个不停。 <br />
<br />
王景说不出妙在何处，只觉心房里锣响铁呜，令人陶醉，令人销魂。 <br />
<br />
余娘弄那老儿阳物，王景吮余娘双乳，老儿香梦中亦见仙女被他梳理，三人 各获所需，其情其状何须多言。 <br />
<br />
且说余娘啊晴一声，便自高处跌下，双腿紧紧铁槽老儿阳物，双眼一翻，死 狗般伏于床上不动，王景没了着落，急得嗷嗷大叫。这下吵醒了老绾，老绾挺身 而起，杀余姐一个回马枪，乒乒乓乓，三五十抽，便大泄如注。 <br />
<br />
王景见父亲精液喷洒后娘后背，点点滴滴．状如蝌蚪，深觉有趣，他说道： “父亲撒的尿象米汤”又见余娘聘了涌流一团白白亮亮稠物，大惊；“父亲，不 好了，后娘他拉稀了，可怪，也是白的！” <br />
<br />
“傻瓜”，余娘死去活来，敲他一个响头：“这是精液，你不懂！” <br />
<br />
王景又欲吮他双乳，余娘以手挡之，道：&quot;尽兴之后，浑身便以没了头，碰也 碰不得。” <br />
<br />
自此以后，他爷儿便宿一床。王景只求余娘玉乳，余娘传授要领，王景一点 既通．王老绾见儿年幼，不以为然，也觉有趣好耍。谁知某夜邻会治史出禁，听 他屋里热闹，便于窗缝往里看，见他爷仨胶成一团，大呼小叫，老叟以为奇事， 逾日说与众人，众人争用传闻，一日不到，大半个京城俱知这件奇趣事儿，真是 ： <br />
<br />
要想人不知，除非已莫为。 <br />
<br />
为贪片刻放，臭了半边城。 <br />
<br />
余娘甚少出门，无缘闻及自家 得凤流事。王老绾初时以为传诵的是人的故事 ，待他详听内容，宜臊得老脸滚烫，慌忙归家谓余娘说了，余娘练唇一撇，说道 ：“关他甚是？这是我家里事，随他嚼烂舌根，我们只管快活！”老绾这才却了 顾虑，任凭他人评说，反正回家后，他仍依然乐成一团。 <br />
<br />
因这事闹得满成风雨，余娘遂想道：“反正银子多．这辈子是花不完的，老 绾年岁流高，大概没有几年快活光景乐，不如劝他告老归乡．购置田舍房产，做 一个阔阔绰绰得土财主，运出做人家奴才自在。还可成天变法儿乐。” <br />
<br />
且说余娘把自家心里合老绾说了， 老绾猛拍大腿，赞道：“娘于高见，我告 老还乡可风光几年，日日守那大门，实在烦闷得紧。”老绾遂辞了守门官职，携 妻带子衣锦还乡，金儿银儿爷跟乐去，王老绾花了千把两银子买置一套在院台三 百多亩田地，果真做个体面乡绅，当地县府亦知他原是严府门官，遂视他为当地 宿老，大凡县府有甚举措，还特地派员投个贴儿与老绾，老绾便仿摩着做官驾式 ，将把胡须。说几句不关痛痒漂亮话儿。 <br />
<br />
遂心如意的事虽不少，但令他心烦的事也不少。 <br />
<br />
且说余娘欲火愈来愈盛，只要闹乐，不分昼暮，只求寻欢尽兴，老绾渐有招 架不住之感。某晚乐罢，老绾突觉下身冰凉，凡不能移，幸亏余娘蒙头睡去，他 便援热麻冷身子，忙了近两个时辰，方能自如走动。正是： <br />
<br />
闭门勤练欢喜功，谁知年迈几不动。 <br />
<br />
此时方知色是刀．无奈心头恋肉红。 <br />
<br />
却说老绾独自悲伤一阵，次日，无心恋战，至县城闲耍，方士麻春知他底细 ，扯着他胡诌：“ 官人福禄一生，无忧无虑，近年威地临运，享尽春色、只是… 。”麻春见老绾神色凛凛，便心喜：今儿又蒙对了！故于紧要处拿捏不语，老绾 摸出二两银子与他，急语：“但说无妨。”麻春揣了银子又道：“只是高寿有限 ，大运逢流年，应在五年之后。不过，乾上祖宗有德，子孙当有紫袍它记，应在 三年后，这便巧了，届时乾君子孙临官而见喜，定可冲减乾拓之凶灾也。”麻春 自家亦不知是何含义。老绾却一一记在心头，一路郁郁不乐，琢磨不已。 <br />
<br />
归家，老绾默想：“五年何其短、想我辛苦一生，当趁有生之年 狂乐享受。 余娘虽是妖娆，来几易人手，熟是熟极。只不鲜矣。待我讨个黄花闺女弄弄，岂 不快活。”老绾拿定主意，是夜，余娘跨于老绾头部，唤老绾吮食阴户之轻水， 老绾欣然咽之，自不免―番耸癫，老绾尽力而为，竟然拍了八百余下，余娘叫死 叫活：“亲亲汉子，乖乖答答，奴家将泄矣！”老绾排出阳物，伏于余娘胯间， 张口以待，热辣辣阴精如洪水涌流，骨骨几声，老绾悉数吃了，老绾亦觉神清气 爽，遂知此物功效非凡，心中一动，道：“娘子，老夫一事相求，不知允否？” <br />
<br />
余娘受活够了，心情舒畅，应道：“夫君乃一家之主，有事但须开口，奴家 焉敢乱纪常。” 老绾忸怩道：“老夫今已五十有四想不久将归冥府，幸逢娘子， 方知人世快活至此，故小老儿乞请娘子本怀大量，乞置一两房小妾，何如？” <br />
<br />
余娘猛听此言，差点气背，只见她花容惨淡，皱额伦眉，似欲发作，老绾心 虚，急切间跪于床上，低语：“娘子休怒！”权当小老儿未说罢乐。” <br />
<br />
余娘沉思：“初时瞧他银子实在，曲嫁于他，又见他阳物稍大，勉合我心， 奴家便敛了骚心，一心一意合他过活，岂料老丑儿得陇望蜀，嫌吾老旧。欲择鲜 货乐，将之奈何？若不允他，如今他有头有面，恼了他，难料不做苟合之事。若 允了他，自家这具皮肉与谁交待，也罢！想他老朽，本已亏空，若添小妾，势必 若决提之水一泄千里，待他黄泉去，我自风流罢。” <br />
<br />
且说余娘思侍良久，脸色由阴转晴，玉手扶老绾起来，温婉而语：“老爷有 有万贯，添几口人本不算甚。妾身曾也有此心，只担心你年高，故隐忍不提，现 今老爷果有此意，妾身当鼎力以助，何敢阻你。” <br />
<br />
老绾心喜若狂，抱紧余浪，顺嘴捏乳，意欲行乐以谢，无奈胯下物疲软勿举 ，眼见一胜美意将化水，余娘阴阴一笑，自荷包里掏出一面帕儿，呵呵笑道：“ 老爷，离京时节，奴家遇从前姐妹，他送我一物，我几忘以，今见老爷不举，方 想起此物妙用。” <br />
<br />
老绾知他前身，亦不怪她，见他手持巴掌大白白净净寻常小帕，不知何用之 有？”他不解道：“寻常之物，于事何补？” <br />
<br />
余娘不言语，跪于床，扶老绾萎萎阳物，张口衔之，却不吮吸，只吐沫，手 指徐之，未见，老绾阳物遍沾湿，余娘将小伯儿缠紧老绾阳物，笑道：“老爷勿 动，此物名曰：‘起阳帕’，不论老幼，着此物襄阳物，片刻即昂举通常，坚如 冷铁，久交方泄。” <br />
<br />
老绾不信，正欲摇头，却觉胯下异常，额首视之，果见自家阳物自那帕儿中 跃出，粗大长硬，宛若一把小锄，非平时之态可比也，只见那物摇摇晃晃，似喝 醉酒的壮汉，一面涨大。一而张望，老结方信此帕神效，遂谓余娘：“娘子当早 就此物，不知其尚能用否？” <br />
<br />
余娘收那“起阳帕”于荷包，曰：“百用百灵，老爷勿忧。” <br />
<br />
余娘仰于床沿，挪枕包垫于臀部，将肥肥美美红红白白妙物高高耸现出来， 两片红唇兀自分开，内中肉眼淫水淋淋，老绾看得大咽口水，他跳落床前，站于 余娘胯间，双手分执余娘小腿扛于肩上，将那小锄似的阳物抵住余娘“小阴茎” ，轻旋轻擦，并不疾速挺人正宫。 <br />
<br />
且说老绾经余娘调教，于那房事之技甚有长进，他抵磨得余娘嗷嗷直叫，却 仍不插进，只把阳物置于外阴，令龟头朝地，自家左右拧动腰肢，只擦得余娘要 死要活，余娘阴唇包住阳物腰身，吞不进，却舍不得吐，骚水如潮，滴滴吐峪， 可巧纳千余娘红绣鞋里，一盏茶工夫，胶样亮水便自鞋语外溢，不题。 <br />
<br />
又说余娘本欲令老绾大泄，因那“起阳帕”乃权宜之物，只管得一时，却管 不了一世，常用者必然折寿，缘何余娘此时方条出夺命法宝，只因醋他欲纳小妾 ．便下狠心把老绾往黄泉道上撵，正是： <br />
<br />
妇心赛蛇蝎，暗施催命符。 <br />
<br />
老绾却道他体已可心，欲施全力报答，细梳慢碾，展胸中学问，轻扣缓擦， 施平生手段，老绾此举又出余娘意外，方知他对自家甚有情意，想到不久之后将 有人分享快活，公里又生若许得意，但明户骚痒奇热，巴巴指望那铁棍儿去捅去 挠，心里又生若许情意来。余娘被他撩拔得魂魄浪荡，便放声喝喊：“亲答答， 入进去吧！入进去吧！奴家痒得要死了。” <br />
<br />
老绾听她言语。知良时既至。便不含糊，挺身耸屁，长驱直入，那棍比比平 时又粗又长，只一捅，便冲至花心，老绾吃一惊：“片刻不弄，他里面却生出瘤 子来！”遂问道。“娘子，痛也不痛，怎的添了物什？”余娘正品尝销魂妙味， 听他可笑言语．呻吟道：“好汉子，你我相交至久，今日才见真心，那物什便是 奴之花心，夫君只管挠它，有工夫，便捉它出来也无妨！” <br />
<br />
老绾听毕，了无顾忌，长身挺选，左右挤弄，上下骚那花心，亦觉自家龟头 被撞得左歪右倒余娘初时只觉浑身通泰，后来却是魂飞魄散，昏死过去了。 <br />
<br />
老绾弄得乏了，喉处浓痰呼呼乱响，却又舍不得停歇，只顾拼命捣鼓，即如 推车上坡，到那至高至陡之处，要么咬牙逾山而去．要么松懈一退千里，正是： <br />
<br />
销魂蚀骨正当时，抵死缠绵逞英豪。 <br />
<br />
可他毕竟老朽，于那极乐峰巅之前，突的一闪，便跌了下去。老绾只觉自家 心底喷出一团热物，源源不绝向外涌去，他知自家阳精泄了，使窃想。“余娘年 岁不大，或可孕子，适他不觉，我便与他下种，说不准生根萌芽，珠胎暗结，那 方上说我子孙临官有喜，莫非应在此时？”老绾此念、为何将那王景排除在外， 只因他知王景愚劣，圣贤书读不得，正经事做不得，故不把入仕为官厚望寄予他 ，可老绾又输余娘一着，因外药激发之精乃无气之精，无法结胎。 <br />
<br />
余娘昏了半晌，悠悠回来，见老绾阳物萎萎然，虽觉还未吃饱，也只得罢了 。 <br />
<br />
话说翌日清晨，余娘导自家绣挂，却见一滩明胶难住了那一双弓鞋，只见外 结厚厚透明物内里红艳可爱，余娘推醒老绾，老绾见之大笑；“真奇事也，闻所 未闻。” <br />
<br />
余娘撒起娇来，要老绾把那堆透明物当早茶吃了，老绾拗不过，只得葡伏在 地，试吮之，触之冰凉，方知其固凝久矣，余娘唤金儿银儿盛之于钵，熬之，拌 人砂糖，老绾果食，甘冽爽口，银耳燕窝不及也，连呼“此物只应天上有，皇帝 老儿不曾食。&quot;王景听了，只道好吃，违争食之。 <br />
<br />
有诗为证： <br />
<br />
东床酣战消淫水，弓鞋盛之不堪容。 <br />
<br />
谁料天凉好个秋．凝冰堆雪宛若玉。 <br />
<br />
熬煎含糖老翁食，却道人间无此物。 <br />
<br />
小儿闻官只管抢．俄顷淫物落肠肚。 <br />
<br />
欲知老绾可否纳了小妾．且听下回分解。</div>

							
							
							
							
