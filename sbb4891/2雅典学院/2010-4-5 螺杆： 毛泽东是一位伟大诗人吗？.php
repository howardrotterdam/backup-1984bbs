													<h2>螺杆： 毛泽东是一位伟大诗人吗？</h2>
						
						
																											<div id="postmessage_548826" class="t_msgfont">《沁园春．雪》一词，据传是胡乔木所写，胡在临死前还专门找中央办公厅的人口诉，曾经展示过原始稿件，特别予以说明。这首词最早是胡为巴结刘少奇的献辞，后来刘为巴结毛，又将此词相赠，毛很是喜爱，加以润色，便窃此词为己有，胡乔木对之至死都耿耿于怀。另一说法，大有可能是毛先作，胡乔木作了更改补充，最后毛据为己有，胡乔木也无法相争。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;我认为，历代帝王是写不出好诗的。比如康熙乾隆，一生诗作上万，差不多一天两首，有多少象样的？除了《沁园春．雪》，毛泽东其它的很多诗也都是身边的御用文人修改而成。毛泽东一生诗词共一百首，但早期发表的不过几十首，大部分都是他死后整理出来发表的，与康熙乾隆相比，不过是个零头。如果说毛泽东是个诗人，一生才写这么点诗词，也够寒酸。文革期间为什么毛诗词烩灸人口？那因为全国人民天天只能读到听他的诗词，而见不到别人的，日久生情罢了。从延安文艺座谈会以后，共产党治下就没人敢发表旧诗，因为古体诗不是“人民群众喜闻乐见的艺术形式”，天底下只有他毛泽东一家长短句，能作诗的大才子都在家里憋着，害怕文字狱祸从口出呢。<br />
<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;论文学基底，毛泽东只读过六年私塾，相当于现在的小学四年，学了点古文旧诗，后来到湖南一师学习，他的自然科学课程数理化等，全不及格。国文也不是全班最好的，只能排在前十名中。他的老师有几个很能吟诗作词，每年组织学生到岳麓山、橘子洲等名胜地方游览，都要大家吟咏一番，毛泽东算是其中一个比较活跃的。后来落草井冈山为寇，从江西逃窜陕北，一路狼狈奔命，哪有情绪写诗？大部分是到陕北以后才写出来的。抗战期间，毛才有了闲情逸致，躲在延安和共魁们一起吟诗看戏纳小妾，被王实味批为：“歌啭玉堂春，舞翻金莲步”。王实味也因此被砍了脑袋。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;这期间，毛偶有诗作，都请董必武、谢觉哉等老学究修改，有时还得益于胡乔木陈伯达等秘书，不耻下问，非常虚心。这种谦逊也是他的作品和权力后来得以成就的原因之一。今天，《沁园春．雪》的定稿和原稿之间到底有多大出入，已经无从知道，但“欲与天公试比高”的“试”是最后确立的，其他之处据胡乔木讲“还多有讹误”。 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;　　 在毛时代，毛诗词被谱成歌曲在全国播放；红卫兵造反派竞相用毛诗句做战斗队名称，什么“换新天”战斗队、“征腐恶”兵团等等。对毛诗和语录的不敬是大逆不道，是现行反革命。有人因为把《毛泽东诗词选》放在睡床席下就被活活打死；有人因折损其中的字句而被判刑。至今尚不见文学评论界对毛诗词做过贬诋，人们从政治上批判毛泽东的同时，也应该对毛诗词做出实事求是的评判。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;　　 比如《沁园春．长沙》，被指为毛的绝唱。毛自称青年时就“对于长短句的词学稍懂一点”，应该是因这首词才有这种自负。但是，有人对毛的少年朋友作了多方调查，发现这首词的原作和公开发表的并非完全相同：<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;“肃立寒秋，湘江北去，橘子洲头。看万山红遍，层林尽染，漫江碧透，百舸争流。鹰击长空，鱼翔浅底，万类霜天竞自由，怅寥廓，问苍茫大地，谁主沉浮？欢歌百侣曾游，忆往昔峥嵘岁月稠。恰同学少年，风华正茂；书生意气，挥斥方遒。指点江山，激扬文字，粪土当年万户侯。曾记否，到中流击水，浪遏飞舟？” <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;　　 据说这首词作是毛在1920年和1921年间与同学游岳麓山和橘子洲头，众人所作的联句，经几个老师修改而成。萧三在陕北获得此词，是毛提供的。但已改“肃立”为“独立”，改“欢歌”为“偕来”。所以此词在功力及风格上和毛泽东先后所作其它诗词大相迥异，其著作权，一直为词学界所怀疑，但知情人慑于毛的权势，都不敢提出异议罢了。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;另外，从一些旁证也可看出其中确有不少问题。第一：《毛泽东诗词选集》刊明该词是1925年作，查当年8月，毛泽东即由湖南启程往广州，9月到达广州参加国民党中央宣传部工作，10月被汪精卫推荐做代理宣传部长。按公历8月当为旧历7月，湖南一带气候尚未降霜，何来“万山红遍，层林尽染”？ 第二：毛泽东当时已为共产党的头面人物，在国民党的地位也不算低，怎会像初出茅庐的后生那样，“问苍茫大地，谁主沉浮”？第三：如果毛泽东当年8月在湖南长沙有此佳作，9月份到了广州应该与在广州的林伯渠、柳亚子、郭沫若等人斟酌，作饮茶谈资，可见毛泽东当时还不敢公然把此词的着作权全部占为己有。第四：到陕北以后，毛泽东仍然没有做为己作公开于世，直到萧三将它录出，才在1957年收录于《诗刊》发表，此中蹊跷，明眼人一推即可全知。 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;再如《咏蛙诗》：“独坐池塘如虎踞，绿荫树下养精神。春来我不先开口，哪个虫儿敢做声。”在韶山毛故居作为毛少年作品展出，现在有人已查出该诗原为清末安徽名士程正鹄所作，毛拈来改了两句，就变成了他的作品。其它的如“风雨送春归”这句，我也曾从一本无名氏的清代诗集中见过。文学诗词作品，免不了有借用引用的时候，但作为诗词，整句的照搬人家的，也应该有个注解，毛泽东诗词中有很多诗句是照搬或修改古旧诗的。举凡照搬改写名家名作的，整理者不得不做说明注释，但无名氏的旧诗旧词，若毛泽东自己不说明出处，不加以注解就有抄袭剽窃之嫌了。至于毛后来的土豆牛肉不须放屁等“诗词”，还有郭沫若臧克家之流大捧领袖臭脚所注释的东西，在这里更不值一提。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;与同时代人相比，汪精卫的诗就要比毛泽东更豪放悲壮，无论在意境深度还是在文字功底上，都要比毛泽东略胜一筹： <br />
