<br />
有吗？没有吗？我觉得和菜头拒绝楼主这样的说法本身，似乎就意味着这样的说法很成问题，佐证是，和菜头应该也是一位老网民，假如互联网存在着任何所谓约定俗成的“契约”，也是众多网友经过长期的、艰苦的敲键盘实践得出的。当然，这只是和菜头个人的行为，我们可以说和菜头是个SB，或者是个没有契约精神的家伙。那么楼主下面的一段话，证明了互联网上存在这种约定俗成的“契约”的说法，至少是还属于有待继续深入讨论、需要（众网民）加以确认的命题了――<br />
<br />
“中文网络上这个话题，二零零三年起就“争议”不断。如今两边都已疲于老生常谈。不过，在我的视界内，我这样的意见，应当从来没有人说过。”（原帖9楼；楼主以为自己首先提出一个崭新命题，牛逼哄哄，结果打了自己的嘴巴~~吼吼）<br />
<br />
看明白了吗？如果不明白，楼主又说――<br />
<br />
“‘在别人 blog 上留言’的性质，在我看来，如无特别声明，就是搞一个开放沙龙，而人们去参加了，只要遵守文明惯例，就不应该被随意驱赶……”（原帖4楼）<br />
<br />
看明白了吗？楼主通篇演绎了一场完美逻辑（都扯到“万国法典了”~~吼吼），但是理论之根却是自设的。自设就自设，但你不能要求别人一定要遵守呀，还强奸民意谓之“约定俗成”（回帖言论显示：至少有相当比例的网民同志不认可这种“约定俗成”的说法）呀？此刻我想到了一个比喻：<br />
<br />
流沙上的大厦……<br />
<br />
这里面没有更高深的玩意，用不着扯什么“万国法典”；你如果要扯什么“自由主义”的话，此刻，我又仿佛看到了和菜头的奸笑――<br />
<br />
我就是要自由地删你的贴~~<br />
<br />
给鉴定结论吧：<br />
<br />
结论一：楼主闲的蛋疼。<br />
<br />
结论二：楼主具有典型的极权思维特征，可能他自己还没有意识到（当然不是每个人都能意识到的，这不需要多高深的学术理论，只需要一点点：智商）。本来我是没有这个结论的，但是经过理出这么一大堆的类似裹脚布的玩意之后，我确实感受到了楼主、以及所有极权者脑子里的那个固有的思维特征――提出一个宏大的、让人似乎无法抗拒的命题，然后所有的谎言由此推出。在这样的命题下的任何逻辑、论证，谎言绝对不是谎言，谬论绝对不是谬论。要推翻它，你只能抓住它的根子，并且，无需任何理论，用事实说话即可。比如，“共产主义是个美好的社会”就是这样的一个命题。（当然这里需要说明的是，“共产主义”是对于未来的一个美好想往，至少现在我们无法确知未来社会是不是“共产主义”形态，但到目前为止，所有以“共产主义”之名的行为已被实践证明为SB；似乎我们现在仍然继续被SB着――这已经不是SB而是一种“阴谋”了~~）<br />
<br />
结论三：我也闲得蛋疼~<br />
