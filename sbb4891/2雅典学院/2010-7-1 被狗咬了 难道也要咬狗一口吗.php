狗，作为主体，其实是人类的朋友；当然也有令人厌烦的咬人的狗，还有为虎作伥的恶狗。我这里所称咬人的狗，还有别于恶狗，我只是不会反口去咬它，而还没有视作为恶狗。如果真是恶狗，打狗棒是不够的，应该用“枪”。<br />
