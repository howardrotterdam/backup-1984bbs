<br />
问题就在于：如何鉴别应该对之使用“枪”的对手！而广场政治中，屡见不鲜的就是对身边的人抡棒子，甚至“开枪”，比如声称掌握了证据有人被政 府收买，却又拿不出证据――这里有些人又要跳了。<br />
<br />
简单说，区分不同意见，和鉴别真正的敌人，是首要问题。一触即跳，容不得任何不同意见，甚至出言不逊，除了缺少教养，更重要的是搅乱了阵营；而这种行为方式，又何尝不是狼奶所哺育的呢？――有些人要二跳了。<br />
<br />
一旦专制垮塌，人民开始选择，彼此相见的若还是棒子和枪――我相信中华大地上将出现远比台湾议会中恶劣得多的现象！这即是我乐观于中共的崩溃，而悲观于民主制度的建立，的原因。前者我期待着，后者怕是三五十年甚至百年之后了。<br />
<br />
现在的论坛，是否可以视为将来民主选择中的预演？至少我做如是观。<br />
<br />
“狗比人多”，我想不是问题。正因为“狗比人多”，人的责任或自律，才更显重要。难道民主化后的中国，也永远是“狗比人多”吗？其实，即便在小组里，我相信还是人比狗多：）只不过狗的叫声大，而有些人不出声罢了。更何况，在“狗比人多”的现状下，总不要是人都变成狗，而是希望狗逐渐变成人。<br />
