工业革命大量增加了人类的物质富裕，但也对传统社会秩序产生彻底的分裂，并带来了新的社会问题如污染、个人与其群体社会的疏离、城市内的拥挤、和童工的出现。物质和科学的进步提升了人类的寿命并减少必死率，使世界人口戏剧化的增长。这也导致了劳工过多的趋势，造成了工资的下降，但也不一定会降低生活的标准。米尔顿&#8226;佛利民指出这个时代造成的现象不是贫穷人口增加，而是“贫穷现象更显而易见”。经济自由主义者如约翰&#8226;洛克、亚当&#8226;斯密认为工业社会会自行调整这些问题而无需政府干预。在19世纪，自由民主制国家的投票权都普遍延伸了，而这批新获得选举权的公民往往投票支持由政府进行干预来解决那些他们日常生活遇到的问题。识字率和知识散布的增加也导致社会上出现更多各种形式的激进主义者。社会自由主义者要求以法律来取缔童工、限制最低工资和工作环境的安全。自由放任的经济自由主义者则反驳认为这样的法律是对生命、自由和财产权的不公平要求，更不用说这样做会妨碍经济的发展。也因此自由主义内部开始产生争议。经济自由主义者强调经济上的自由，并希望缩小政府的规模。然而社会自由主义者强调的是机会的均等，并希望扩大政府的规模以保护公民免受经济和自然状态的困难所产生的后果，他们认为那些问题的严重只能由政府来解决。在19世纪中旬社会自由主义因此和古典自由主义分裂，成为自由主义内部第一次意义重大的分裂。 <br />
