													<h2>老鼠的启蒙教材之三：经济增长的原因是什么？</h2>
						
						
																											<div id="postmessage_143234" class="t_msgfont">新熊彼特主义者认为，创新不是经济增长的原因，创新本身就是增长。那么经济增长的原因是什么呢？新熊彼特主义者认为经济增长的原因是制度创新。制度创新的两个“关键词”是界定产权和降低交易成本。其实界定产权也是为了降低交易成本，因此我们主要谈谈后者。<br />
<br />
&nbsp; &nbsp; 交易成本就像电阻一样：输电线路的电阻只要下降一点，传输的电流就能增加不少，交易成本也是这样：交易成本只要下降一点，人类的经济和社会发展就能迈进一大步。因此对于电阻和交易成本，人们都希望它们越小越好。电阻为零的现象叫做超导，一般发生于金属处于极低温度之下时。而交易成本为零的现象可能永远也不会发生，但是我们可以想象一下假如交易成本为零会发生什么事。<br />
<br />
&nbsp; &nbsp; 对于今天的中国来说，交易成本为零最可能的结果之一就是中国民主转型的发生。中国的民主转型不能发生，原因就在于转型所需要花费的交易成本太高，如果交易成本为零的话，所有各方想必能够通过谈判达成一个各方都满意的转型协议。<br />
<br />
&nbsp; &nbsp; 技术创新导致的交易成本的降低使制度变迁成为可能。例如有学者认为，苏联解体就是电视普及导致政治交易的成本降低所致。还例如，最近的厦门PX事件显示，互联网和手机技术不仅使民众更容易形成共识，也使民众和政府之间的对话和谈判更容易进行，从而降低了政治交易的成本和制度变迁的门槛。制度变迁又使技术创新和经济增长能够持续进行。例如专利制度、公司制度，以及经济领域内的种种制度，都能够起到鼓励和促进技术和管理等方面的创新，从而保证经济持续发展的作用。因此如果幸运的话，制度创新和经济增长会进入互相促进的良性循环，从而使经济持续增长。不幸的话，制度变迁和经济增长也可能都会陷入停滞，甚至陷入恶性循环而无法自拔。关于后者，安・兰德在其小说《阿特拉斯耸耸肩》中生动地描写了这一过程：国家对经济的干预这一“制度变迁”如何陷入干预范围越来越大、干预程度越来越深的“干预螺旋”，迫使社会不得不投入越来越多的资源用于和官员“拉关系”，致使整个社会的交易成本节节攀升，那些支付不起这一成本的企业大量破产；技术创新和企业家精神得不到奖励，反而受到惩罚，促使人才和资本大量“逃离”；最后导致整个国家的经济濒临崩溃。<br />
<br />
&nbsp; &nbsp; 国家可能害怕由交易成本的降低导致的政治变革，因此采取了种种手段来封锁和控制互联网和手机短信，其本质无非是重新抬高交易成本，使得政治交易和政治变革难以进行，但是抬高交易成本也会阻碍经济的发展和社会的进步，他们难道不害怕《阿特拉斯耸耸肩》中所描述的经济崩溃的可怕后果吗？</div>

							
							
							
							
