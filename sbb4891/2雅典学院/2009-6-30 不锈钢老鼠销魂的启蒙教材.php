																											<div id="postmessage_143233" class="t_msgfont">《老鼠的启蒙教材之一：财富的来源是什么？》一文中提到这样一个例子：有这样一个农业小镇，镇上每家人都有一块土地，土地上的产出只够自己一家生活，镇上也没有多余的土地。有一个没有任何技术的年轻人到这个镇上来找工作，小镇上的居民给他买了一张回程车票；这个小镇上“没有工作”。与此同时另一个人也到这个小镇上来找工作，这个人是一个农学家，他发明了一种新的农机，能够把土地的产量提高50%。这个人就成了小镇上第一个专门出售农机的人。小镇居民买了他的农机后生产了更多的粮食，他们把多余的粮食卖掉，用这笔钱雇了一个教师来教他们的孩子读书……小镇逐渐繁荣了起来。<br />
<br />
 <br />
<br />
&nbsp; &nbsp; 这个例子还说明：经济增长的本质是创新。经济学家熊彼特认为，经济活动中的创新包括：1、引入一种新产品；2、采用一种新的生产方法；3、开辟新市场；4、获得原料或半成品的新供给来源；5、建立新的企业组织形式。熊彼特认为创新是经济增长的原因。不过后来的新熊彼特主义者认为，创新不是经济增长的原因，创新本身就是增长。<br />
<br />
 <br />
<br />
经济增长是通向开放社会之路<br />
<br />
&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp; 经济增长的本质是创新，这对我们有什么意义呢？卡尔・波普尔在《历史决定论的贫困》一书的序言中提出如下命题：（1）人类历史的进程受人类知识增长的强烈影响。（2）我们不可能用合理的或科学的方法来预测我们的科学知识的增长（如果我们今天知道了自己明天会知道哪些知识，那我们今天就已经知道这些知识了，这些知识就是今天的知识而不是明天的知识了；换句话说，明天一定会有我们今天未知的知识）。（3）所以，我们不能预测人类历史的未来进程。我认为以上命题在创新问题上也同样成立：在创新得到市场的检验之前，我们无法预测哪些创新会成功并且具有市场价值（经济学家林毅夫提出过两个数字：全部研发项目中只有5% 能够成功，而成功的研发项目中只有10%具有市场价值），因此我们也无法预测经济增长将会给我们的社会带来哪些深刻的变化。经济增长并不仅仅是大家口袋里的钱变多了那么简单（如果别的东西都没有改变，只有大家兜里的钱变多了，那是通货膨胀），它还是一条通向开放社会之路。<br />
<br />
 <br />
<br />
&nbsp; &nbsp; 互联网在中国兴起并且给中国社会带来深刻变化（决不是中共所希望的变化）之后，有些人认为中共在九十年代引入互联网是犯了一个错误。殊不知如果没有互联网以及随之而来的新经济，就没有九十年代末开始的经济高速增长，没有经济增长就不能解决就业，失业多了就会影响稳定……尤其是高新技术产业能够解决大学毕业生的就业，而大学毕业生失业对社会稳定的影响要比农民工失业大得多。因此如果说中共犯了错误，那么从十一届三中全会把“以经济建设为中心”确立为基本路线的时候起，这个错误就已经犯下了。朝鲜可以把自己的老百姓饿死，也可以把互联网拒之门外，但是中共已经无法这样做了，哪怕互联网会给它带来真正的威胁，因为倒退的代价是它所支付不起的。中共处于进退维谷的境地中：既然经济增长的本质就是创新，那么中共就不可能只要经济增长，而不要创新以及创新给社会带来的无法预测的深刻变化。</div>

							
							
							
							
