<strong>老鼠的启蒙教材之一：财富的来源是什么？</strong><br />
<br />
&nbsp; &nbsp; 经济制度上应该选择自由主义还是（民主）社会主义，取决于一个基本假设：财富是人类创造出来的，还是老天（或者上帝、大自然，随便你管它叫什么）给予我们的。如果财富是人类创造出来的，那么谁创造出来的财富就应该归谁所有，这样选择自由主义就是理所应当的；如果财富是上天给予我们的，那么我们唯一的任务就是尽可能平等地分配这些财富，这样自然应该选择（民主）社会主义。<br />
<br />
&nbsp; &nbsp; 自由主义者认为财富主要是人类创造出来的，原因如下：<br />
<br />
&nbsp; &nbsp; 1. 即使说上天给予了我们财富，其数量也是很少的。大自然给予我们的财富只能养活与今天的人口相比数量很少的狩猎-采集者，他们几乎完全依靠自然提供的财富来生存，过着在我们今天看来十分可怜的生活，平均寿命很短。今天，同样的地球养活着几十亿人口，他们拥有与狩猎-采集时代相比十分巨大的财富。这些多出来的财富是从哪里来的？不是自然给予我们的，而是人类创造出来的。<br />
<br />
&nbsp; &nbsp; 2. 资源是人类创造出来的。有些社会主义者和环保主义者认为资源是大自然给予我们的，而且总量是有限的。但事实上宇宙中的物质和能量总量及其巨大，被我们所开发利用的只是其中极其微小的一部分。哪些物质和能量能够成为资源从而变成财富，则取决于人类的开发利用水平。没有农业技术，土地上只能产出很少的食物；没有冶炼技术，铁矿石只是普通的石头；没有核电厂，铀也不是什么重要资源；没有计算机技术，硅只不过是沙子而已。世界上最重要的资源存在于我们的头脑之中。<br />
<br />
&nbsp; &nbsp; 3. 经济活动的本质是创造财富。《赤裸裸的经济学》一书中举了这样一个例子；有这样一个农业小镇，镇上每家人都有一块土地，土地上的产出只够自己一家生活，镇上也没有多余的土地。有一个没有任何技术的年轻人到这个镇上来找工作，小镇上的居民给他买了一张回程车票；这个小镇上“没有工作”。与此同时另一个人也到这个小镇上来找工作，这个人是一个农学家，他发明了一种新的农机，能够把土地的产量提高50%。这个人就成了小镇上第一个专门出售农机的人。小镇居民买了他的农机后生产了更多的粮食，他们把多余的粮食卖掉，用这笔钱雇了一个教师来教他们的孩子读书……小镇逐渐繁荣了起来。<br />
<br />
&nbsp; &nbsp; 综上所述，财富的源泉在于人类的智慧和创新能力，财富应该属于那些创造它们的人。</div>

							
							
							
							
