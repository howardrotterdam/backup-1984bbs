													<h2>老鼠的启蒙教材之四：国家的本质是暴力</h2>
						
						
																											<div id="postmessage_143236" class="t_msgfont">自从有了卢梭的《社会契约论》之后，好多人就觉得国家和民间社团没有什么区别：二者都是建立在契约的基础之上的。其实卢梭所说的社会契约，只是民主国家的喻体而已，并不是真正意义上你可以自由决定签或者不签的契约。在卢梭的社会契约中，“每个人都放弃天然自由，而获取契约自由；在参与政治的过程中，只有每个人同等地放弃全部天然自由，转让给整个集体，人类才能得到平等的契约自由。”由此可以看出，“社会契约”，并不是每个人自愿签署的，这与今天我们所理解的契约和契约自由（个人自愿地与他人订立契约的自由）有很大差异。<br />
<br />
&nbsp; &nbsp; 诺奇克的反契约论国家理论对国家的本质论证得更为透彻，诺奇克认为国家的诞生分为三步：<br />
<br />
&nbsp; &nbsp; 第一步所达到的安排被诺奇克称之为“支配的保护性社团”。诺奇克提出，在“自然状态”中，每个人都有保护自己、惩罚犯罪和索取赔偿的权利。为适应人们的安全需要，某些专业性保护机构便产生了。它们出售保护服务，为委托人索取赔偿和进行惩罚。在一个地区之内，最初可能存在着许多这样的专业保护机构，但由于相互竞争，最终一个地区会出现一个击败其他机构的“支配的保护性社团”，它承担此地区内的全部保护性服务。<br />
<br />
第二步所达到的机构是“超弱意义的国家”。在存在着唯一一个“支配的保护性社团”的地区之内，一些人付钱给这个保护机构，以购买保护服务。同时也存在着许多“独立者”，他们不想加入或者没有能力加入保护性社团。这样当“独立者”与“委托人”之间发生伤害时，麻烦就产生了。诺奇克认为，如果“独立者”以一种可靠的程序来实行恰如其份的惩罚，那么他有权利这样做，其他任何人或机构都无权干涉。但是，个人行动的程序是非常不可靠的，常常会错误地惩罚一个无罪者，或者对有罪者惩罚过份，从而在人群中造成恐慌。为了保持安定，这个地区的“支配的保护性社团”将禁止任何个人的报复行为，由它自己垄断全部惩罚和索取赔偿的权力。这种安排被诺奇克称为“超弱意义的国家”（ultraminimal state）。但他认为，“超弱意义的国家”还不是真正的国家，因为它只为付钱的委托人服务。<br />
<br />
第三步才是完全意义上的国家的诞生。“超弱意义的国家”是依靠强力来获取运用强力的垄断权的，严格说来，这种权力并不合法。“超弱意义的国家”无权禁止 “独立者”在自己受到伤害时进行报复和索取赔偿，无权强迫“独立者”放弃强行正义的权利。仅仅根据“独立者”行动程序的不可靠性来禁止他们运用报复，剥夺他们保护自己的权利，这种理由并不充分，也损害了“独立者”的利益。因此，“超弱意义的国家”要使自己对强力运用的垄断成为合法的和合乎道德的，就给予“独立者”以赔偿。这就是诺奇克所谓的“赠偿原则”。如果“独立者”被禁止强行正义需要赔偿，在诺奇克看来，最省钱的赔偿方式就是为“独立者”提供免费保护服务。这种既拥有强力运用垄断权又能为所有公民提供保护的机构就是“最弱意义的国家”。国家由此便真正诞生了。（姚大志：《国家是如何诞生的？――美国新自由主义与社会契约论》）<br />
<br />
&nbsp; &nbsp; 简单说来，国家就是一个地区的暴力垄断者。我们之所以要求国家应该有民主、宪政和法治，不是因为它是社会契约的产物，而是因为它垄断了一个地区的暴力，并且禁止私人的暴力行为。对于这种垄断的暴力工具，我们必须用种种手段加以限制和约束。我们之所以要“把统治者关进笼子”，不是因为我们自愿地跟统治者签订了一份契约，而是因为统治者手中拥有暴力，作为个人，我们无法拒绝其统治，我们无法自由退出。<br />
<br />
&nbsp; &nbsp; 由此我们可以看出，国家和民间社团有根本区别：民间社团是自愿加入、自由退出的。民间社团不可能用暴力对待你或者强迫你什么，因为你可以自由退出。因此从理论上说，民间社团与其成员之间的关系适用契约自由的原则。民间社团只要不违反国家法律，不侵犯其成员的生命、自由、财产等基本权利，社团内部无论怎样做，都属于结社自由的一部分，国家无权干涉。社团内部可以有详细而明确的规章制度，也可以只有大致的原则而没有细则，甚至可以没有成文的规则，把自由裁量权完全留给决策者；社团可以有内部监督机制，也可以没有；社团在决策时可以是民主的，也可以是独裁的，还可以采用共识决策……重要的是社团必须允许成员自由退出，这样市场机制就可以完成对社团进行监督和优胜劣汰的任务。这就是西方自由社会中的规则。<br />
<br />
&nbsp; &nbsp; 有些国人习惯把自由社会中的社团当作经典社会主义体制下的“单位”。但中国过去那种“单位”其实相当于一级政权组织：个人很难离开单位，单位领导有权过问你的私人生活，甚至单位领导签字就可以把你送去劳教。由于“单位”相当于一级政权组织，人们自然会希望单位内部在决策上应该是民主的，对单位领导应该是有监督的，也自然会有人花费大量时间精力“与领导作斗争”――这都是因为单位很难自由退出。但是这些期望和做法并不适用于自由社会中的社团：如果一个社团不符合你的期望，退出就是了。比如高寒告笔会的案子，高寒郭罗基等人要求笔会在因为“诽谤”而开除高寒的时候必须按照国家关于诽谤罪的立法来做，必须给予被开除者申诉的机会，或者必须有关于如何判断是否构成诽谤的细则等等，这些都是对国家的要求，民间社团并不是必须按照上述要求来做。如上所述，国家与民间社团有本质区别。还有人以为笔会会长可以禁止会员在网上发表某些言论，这是把笔会当成单位了，笔会章程中没有关于会长有权禁止会员发表某些言论的内容，因此会长即使表示他不希望我们说哪些话，我们也不会听他的。</div>

							
							
							
							
