													<h2>老鼠的启蒙教材之五：什么是通货膨胀</h2>
						
						
																											<div id="postmessage_143237" class="t_msgfont">我小时侯曾经思考过这么一个问题：既然大家都想要钱，那政府为什么不印很多钱发给大家呢，这样大家不就都有钱了吗？<br />
<br />
&nbsp; &nbsp; 当然，这种观点肯定是错误的：大家想要的不是钱，而是钱能够买到的东西。每一块钱都能够换到一定数量的物质或精神财富。事实上如果不是每一张纸币都对应着一定的财富的话，纸币就是些废纸而已。如果一个社会中的“钱”增加了而相应的财富总量没有增加的话，每一块钱能够买到的东西就减少了。货币贬值，商品价格上涨，这时通货膨胀就发生了。<br />
<br />
&nbsp; &nbsp; 通货膨胀是如何发生的呢？过去人们使用金银作为货币，这样如果某人发现了新的金矿或者银矿，市场中流通的金银增加了，这也会导致金银贬值，其他商品价格上涨。挖掘金银矿者得到的收益是以其他人手中的金银贬值为代价的，这就相当于挖掘金银矿者变相抢劫了其他金银持有者手中的财富。但是就整体而言，采矿业的收益率并不高于其他行业，而且金银除了作为货币之外，还有作为普通商品的用途，如做首饰等，因此金银采矿并不会给社会造成很大麻烦。<br />
<br />
&nbsp; &nbsp; 各国的货币原本都是金银，例如一英镑原本是指一磅白银，一美元原本是指二十分之一盎司黄金。但是在进行大宗交易的时候，金银可能显得过于笨重不便携带，因此人们把金银存入银行，得到存单，这样人们可以直接使用存单进行交易，得到存单者可以到银行提取金银。纸币的来源就是这种存单。纸币一开始并不是由国家发行的，而是由私人银行发行的。纸币本身也并不是真正的货币，它只是证明你有多少货币（金银）的凭证而已。<br />
<br />
&nbsp; &nbsp; 就银行来说，银行本该存有多少金银就发出多少存单，但是银行发现存单持有者一般不会在同一时间来把他们存入银行的金银提走，因此即使银行发出了比存有的金银数更多的存单，别人也很难发现。银行可以把多出来的存单作为贷款发放出去收取利息，这时比原有的金银数量更多的存单流入了市场，这就造成了存单（纸币）的贬值。但是由于银行之间存在竞争，而且如果多发的存单过多，在有相当比例的存单持有者同时要求提走存单上的金银的情况下，银行有破产的危险，因此由于银行发出比实际拥有的金银更多的存单（纸币）而导致的通货膨胀，其程度也是有限的。<br />
<br />
&nbsp; &nbsp; 到了二十世纪下半叶，政府不仅垄断了纸币发行，还逐渐使纸币与金银脱钩。现在政府发行纸币时再也不需要有金银作为抵押物了，政府可以随意多发纸币而不用担心由于纸币持有者要求用纸币兑换金银导致的破产。政府是愿意搞通货膨胀的，通货膨胀是一种变相税收，政府只要开着印钞机就可以源源不断地抢劫纸币持有者，从纸币的贬值中获得财富。通货膨胀对政府和与政府关系密切者有利：这些人最早拿到多印出来的纸币，他们拿着这些纸币去购买商品时，商品价格还没有开始上涨。通货膨胀对远离政府权力者最不利：商品价格上涨之后这些人才能拿到多印出来的纸币。这样工薪阶层和退休者往往成为通货膨胀的最大受害者。政府有时会告诉你适度通货膨胀有益，别忘了这句话是谁说的！<br />
<br />
&nbsp; &nbsp; 政府会说通货膨胀有利于提高就业，这不是真的。某人失业是因为市场上能够提供的工资水平低于某人能够接受的工资水平。当通货膨胀刚刚开始的时候，某人可能发现市场提供的工资水平涨到了高于他的预期的水平，他高兴地就了业，但是不久之后他就发现物价也上涨了，他的工资上涨只是一个幻觉，实际上他的工资还是低于他的预期，他是被政府骗得就了业，现在他开始考虑辞职了。政府采用通货膨胀政策来提高就业，一开始确实如愿以偿，但是后来越来越多的人发现是政府在搞鬼，因此不再上当，这时通货膨胀政策就只能提高通货膨胀率，不能提高就业，滞胀就是这么产生的。<br />
<br />
&nbsp; &nbsp; 通货膨胀不仅能够欺骗工人，也能够欺骗资本家，让他们以为自己的投资收益率高于预期，因此进行更多的扩张，造成经济的表面繁荣，但是经济在繁荣时期爬得越高，在随之而来的萧条时期摔得就越惨。因此哈耶克认为，经济周期和经济危机并不是资本主义的必然产物，而是政府垄断货币发行和通货膨胀政策的必然产物。<br />
<br />
&nbsp; &nbsp; 为了应对中国目前的经济衰退，中国政府出台了四万亿刺激经济计划。该计划极有可能以政府发行债券，然后由央行购买的方式来获得资金。这就是一种通货膨胀政策。政府希望以这种方式来造成经济的虚假繁荣，但我认为这种政策只会造成滞胀而已。<br />
<br />
&nbsp; &nbsp; 对政府来说，通货膨胀只有一个真正的危险，那就是恶性通货膨胀最终能够导致政权垮台，1949年国民党政权撤离大陆之前就发生过这种情况。<br />
<br />
