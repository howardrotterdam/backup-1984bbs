													<h2>不锈钢老鼠的启蒙教材之六：经济活动的目的是什么？</h2>
						
						
																											<div id="postmessage_143240" class="t_msgfont">有朋友曾经问我：你们主张自由主义，究竟是因为自由本身就是好的，还是因为自由最能促进经济发展？如果是前者，那么自由主义就是不可证伪的；如果是后者，那么要是别的制度比自由更能促进经济发展，是不是就应该放弃自由主义呢？比如前苏联和现在的中国，经济发展速度似乎都快于同期的资本主义国家。<br />
<br />
&nbsp; &nbsp; 这让我想到了本文要谈的问题：经济活动的目的是什么？前苏联的经济发展数字似乎不错，但是人民生活困难，这种经济发展是我们想要的吗？<br />
<br />
&nbsp; &nbsp; 我的观点是：经济活动的目的是为了满足人的需要，而不是为了GDP数字好看。消费当然是为了满足需求，投资也不过是为了满足将来的需求而已。这一观点我想没人会反对吧。<br />
<br />
&nbsp; &nbsp; 需求的特点之一是，每个人的需求是不同的。萝卜白菜各有所爱，一个人的美酒佳肴就是另一个人的穿肠毒药，说的都是这个道理。因此交换能够创造财富：我有一个苹果，你有一个橘子，但是你爱吃苹果，我爱吃橘子，那么咱俩一交换，虽然还是一个苹果一个橘子，但是它们满足需求的程度提高了，我们就可以说财富增加了。只要是双方自愿的交换，都能够创造财富，因为在双方自愿的情况下，只有交易双方都认为得到的东西比失去的更能满足自己的需要，交易才能实现。<br />
<br />
&nbsp; &nbsp; 人类需求的多元化特点，决定了人的需求无法用中央计划机制来满足，因为中央计划者无法确切地知道每个人的需要究竟是什么。只有自由市场才能让每个人通过自愿交换来满足自己的需要。只有自由才能创造真实的、能够满足人的需要的财富。<br />
<br />
&nbsp; &nbsp; 那为什么非市场经济国家也有经济增长，有时甚至比市场经济国家增长更快呢？大家都看过这样一个笑话：<br />
<br />
&nbsp; &nbsp; 两个经济学家(师兄弟关系)在公园散步，说着走着……突然发现路上有一坨狗屎。师兄对师弟说：你把狗屎吃了我就给你5000万！师弟计算下觉得5000万吃一坨屎还是相当划算的。于是成交，师兄给师弟5000万支票。接着走，又发现一坨。师弟咂咂嘴巴，琢磨着狗屎的味道确实不太爽的说，就对师兄说了：你要是敢吃了，我也给你5000万。师兄正心疼着自己那5000万块钱，见师弟如此说，当下吃了个干干净净。于是那5000万重又回到师兄的荷包。又走下去，师兄师弟相拥大哭：一分钱没有挣到，一人吃了一坨屎啊……回到师傅面前，老教授一听，激动地颤颤巍巍、哆哆嗦嗦：一个亿呀，一个亿！你们俩什么也没干就给国家增加了一个亿的GDP。<br />
<br />
&nbsp; &nbsp; 这个笑话在现实生活中不可能发生：如果一个人吃狗屎赚了5000万，恐怕不会愿意因为对方吃狗屎再把这5000万还给他。试想，假如师兄对师弟说：如果你吃一坨狗屎，那我也吃一坨狗屎，这个交易师弟会接受吗？如果你认为不会的话，那么上述笑话也就不可能发生。在双方自愿交易的情况下，用这种方法来创造GDP 是不可能的。<br />
<br />
&nbsp; &nbsp; 但是，国家却有可能抢走你的钱，强迫你吃狗屎，同时声称创造了GDP。例如，国家可以强制你购买软件用来监控你自己，同时创造4000万的GDP。但是这种“交易”不是双方自愿的，因此它没有满足购买者的需要，也就没有创造真实的财富。类似的例子还有很多。例如有人说：如果把一座楼拆掉再盖一座一模一样的，能够创造出很多GDP，但楼还是跟以前一样，什么新的财富都没有创造出来。这个例子也是一样：在自愿交易的前提下，这种事情压根不会发生；但是国家却可以强制拆除你的房子，建造在自愿交易的情况下很可能因为得不偿失而无人愿意建造的奥运场馆或三峡大坝，同时创造GDP。GDP不是财富，只是衡量财富的指标而已。国家用这种方法来创造GDP，就像为了蛋白质指标合格而往牛奶里加三聚氰胺一样。<br />
<br />
&nbsp; &nbsp; 社会主义体制（指前苏联东欧国家和1978年之前中国的体制）下，衡量经济发展的指标不是GDP，而是各种产品的产量。由于产品种类太多，中央计划者注意不过来，因此实际上用来衡量经济发展的指标是重点产品（如钢材、粮食等）的产量。市场经济国家衡量经济发展的指标是GDP（国内生产总值）和GNP（国民生产总值），社会主义国家不统计GDP和GNP，要想与市场经济国家做比较，就只能比较某些重点产品的产量。这样，社会主义国家为了证明社会主义制度的优越性，往往下大力气生产重点产品，而忽视了非重点产品的生产，在这种情况下，重点产品的产量往往不能真实反映一个国家的实际经济发展水平。而且，用来衡量经济发展的重点产品往往是重工业产品，而人们需要的各种消费品会被当作非重点产品忽视，这样的所谓经济发展不能满足人们的需要，实际上也相当于三聚氰胺牛奶。极而言之，这种三聚氰胺式经济发展的例子就是中国的大炼钢铁：为了钢产量“超英赶美”而把各家各户本来有用的铁锅抢来，“炼”成没有任何用处的废铁。这种做法也可以统计为经济发展，但实际上不过是笑话而已。<br />
<br />
&nbsp; &nbsp; 有人曾经批评国内反对三峡工程的人，说美国建了比中国多得多的水电站，你们为什么不反对？现在我们可以回答这个问题了：在美国修建水电站或其他任何东西，如果损害了他人的利益，那么修建水电站的一方必须与所有利益受损者就补偿问题达成一致；如果无法达成一致，那么这个水电站恐怕是无法建成的。像三峡建设时那种强制拆迁移民的做法，在美国恐怕是不会发生的。因此美国的水电站没有损害他人利益，而且创造了财富，当然不应该受到批评。中国的三峡工程满足了一些人的需要，但是侵犯了另一些人的利益，我们无法判断该工程是究竟得大于失，还是得不偿失。我只能说：如果一个工程拿出足够的钱来让人们自愿非强制地搬迁就会亏本，那么这个工程就是不应该建的。</div>

							
							
							
							
