这种惩罚制度有点类似中国古代的流放，当然二者性质截然不同，前者是公民大会的集体投票，后者是专制君主的个人意志。不过，在&quot;贝壳放逐法&quot;这座祭坛上，固然有独裁者的鲜血，也飘荡着无辜者的冤魂。在古希腊历史上，曾经有多位优秀的政治家、军事家因&quot;贝壳 放逐法&quot;而被流放，客死他乡，比如著名的马拉松战役英雄亚利斯泰提，以廉洁、正直而著称，就曾被贪婪、腐败的地米斯托克利以&quot;企图独裁&quot;的罪名提交公民大会审判。<br />
<br />
古希腊的这个&quot;贝壳放逐法&quot;尽管 打着&quot;大多数&quot;的幌子，但却是个很可怕的东西，并不能算做真正的民主，实际上，它有另外一个名字，叫做&quot;大多数暴政&quot;。</div>

							
							
							
							
