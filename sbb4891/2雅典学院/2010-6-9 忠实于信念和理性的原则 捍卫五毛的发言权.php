													<h2>忠实于信念和理性的原则 捍卫&quot;五毛&quot;的发言权</h2>
						
						
																											<div id="postmessage_668112" class="t_msgfont">1984从前是怎么样的, 我没有经历过.<br />
&nbsp; &nbsp;&nbsp;&nbsp;1984的今天, 却不怎么样.<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;会员间新见面, 互相问候交流的不是观点, 而是用警惕的目光审视对方,推敲对方...然后从兜里的两顶帽子中挑出一顶,为对方戴上... 很不幸...我被戴上了五毛的帽子 :-)<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 2002年在铁血大战历史唯心主义者时, 我被戴过汉奸的帽子, 04年在天涯喷自由主义和共产主义时,我被戴过特务的帽子, 06年在爱卡上分,我被扣上美分的帽子, 当我被很多网站封ID,删贴,被新浪,搜狐封博的时候, 我倒没有想过, 某天我竟然会成为别人口中的&quot;五毛&quot;...而原因仅仅是, 我维护&quot;五毛&quot;说话的权利,尽管我不认同他们的观点.<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;文革的时候,右派,反革命的帽子是批斗的依据, 而在1984,这顶&quot;五毛&quot;的帽子和当年的&quot;右派&quot;,&quot;反革命&quot;并没有什么本质的区别...你不是极右,就是&quot;五毛&quot;... 比如我这种情况, 放到当年, 就应当被划入&quot;修正主义分子&quot;的行列...你革命不够彻底,你同情&quot;敌人&quot;, 于是你也就成了敌人... 基于这样的逻辑, 鉴于我也成了&quot;敌人&quot;, 我对把我当成敌人和五毛对待的&quot;民主爱好者&quot;所信仰的&quot;伪民主&quot;,就实在没什么好感了...<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;安.兰德是我喜欢的作家,她说过: &quot;如果一个人从一开始就发现很难保持对自己信念的忠诚，那么一连串的背叛就不会让他以后更容易保持忠诚，却会让他完全不可能保持忠诚。&quot;&nbsp; &nbsp; <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;所以, 假如某人对于自己所信仰的民主原则进行了背叛, 那么他就失去了对民主的忠诚, 不知不觉中堕落到了独裁的深渊之中.我1997年开始上论坛,2001年起逐渐形成了成型的意识形态和民主价值观,从此不断去捍卫,参加过无数场BBS论战,无数次被骂娘,很多次被封号,封IP,事后我耸耸肩,我觉得他们赶走我,是因为害怕我...我从来没有因为遭受过的不公,而想要把这份不公加诸到别人头上, 我这么做了, 也就和我看不起的人一样嘴脸了...<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;今天你把五毛的帽子扣到对方头上,然后要将对方赶走,禁言... 沉浸在这个小世界中,享受&quot;民主&quot;给你带来的&quot;乐趣&quot;... 但那究竟是什么样的乐趣呢? <br />
难道民主就是一群情投意合的人一起控诉这个黑暗的社会? <br />
难道胆量就是不由分说给人扣上五毛的帽子指着对方的鼻子骂娘? <br />
<br />
然后大多数人还是该回家回家,该被共产党操一样被共产党操,在现实生活中连屁都不敢放一个...<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;所以,假如你没有胆量在这个残酷的社会中为自己争取公民的权利,那么,至少在网络上,请坚持自己的理想,请保持诚实,遵循原则.---------&quot;诚实的含义并不是指忠于自己的主观奇想，而是指忠实于理性原则。&quot; --------这句话同样是安.兰德所说. ---- 如果你有原则的话. 要做到这一点, 其实真的比操逼骂娘乱扣五毛的帽子要去赶人走路更艰难...一个人抛开自己的信仰去发泄情感,其实是一件很容易的事情...<br />
&nbsp; &nbsp;&nbsp; &nbsp;<br />
&nbsp; &nbsp;&nbsp; &nbsp;我想,要让1984变得更好,所有人都应当有发言表达自己观点的权利. 哪怕你不认同他的观念. 甚至你可以骂他(这是你应得的权利),但不要乱扣帽子,让人闭嘴... 1984bbs并没有&quot;不准发表某某观点&quot;的版规...<br />
