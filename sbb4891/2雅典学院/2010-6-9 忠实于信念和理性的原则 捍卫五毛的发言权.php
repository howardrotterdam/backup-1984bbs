																											<div id="postmessage_668457" class="t_msgfont">公元前六世纪，在民主的发源地――古希腊城邦雅典，一个名叫克利斯梯尼的政治家发明了一种人类历史上最早的民主制度的雏形：&quot;贝壳放逐法&quot;。所谓&quot;贝壳放逐法&quot;，就是雅典人为了对付某个破坏民主、实施专制的独裁者，可以召开公民大会，对其进行投票（因用贝 壳投票而得名，不过后来改用陶片了），如果这个人得票超过6000，那么对不起，管你有没有错，立即离开雅典，去外面呆上10年再回来吧。<br />
