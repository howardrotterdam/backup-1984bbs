													<h2>余杰 ：以基督信仰拯救中国的“不文明史”</h2>
						
						
																											<div id="postmessage_593001" class="t_msgfont">萧建生的《中国文明的反思》一书，用民主、宪政、人权等现代文明观点对中国文明作了系统、全面的反思，作者的态度温和、理性而富于建设性，对当代部分涉及并不多，却在上市前夕，被宣传部下令查禁，最终“胎死腹中”。这样一本好书，只能转移到香港出版，这究竟是作者的悲哀，还是这个国家的悲哀？这样一个没有起码的言论自由和新闻出版自由的国家，有什么资格去参加德国的法兰克福书展，并企图向世界输出“软实力”呢？<br />
<br />
<br />
分裂为何比统一好<br />
<br />
<br />
&nbsp; &nbsp; 20世纪90年代以来，在官方鼓励下的复古主义思潮甚嚣尘上，国学热、风水热、帝王热、祭祀热等等，此起彼伏，一浪高过一浪。然而，在这些表面的热热闹闹背后，却是另外一番景象：现实生活中，道德沦丧、文化荒凉、斯文扫地，教授与官僚一样无耻、作家与商人一样贪婪。尚有良知者，无不对中国文化的前景感到透骨的悲凉，此种感觉正如陈寅恪在《王观堂先生挽词序》中所说：“凡一种文化值衰落之时，为此文化所化之人，必感苦痛，其表现此文化之程量愈宏，则其所受之苦痛亦愈甚。”萧建生没有“举杯浇愁愁更愁”，而是继续80年代知识份子在《河殇》中反思黄色文明、呼吁蔚蓝色文明的思路，并大大往前走了一步：他意识到，仅有政治制度和文化思想层面的变革是不够的，政治制度和文化思想的背后是宗教信仰，由于缺乏基督教信仰，中国两千年所谓的文明史，其实充斥着“不文明”的因素，用鲁迅的那句老话来说就是，写满了“吃人”两个字。如今，是到了用基督信仰来拯救中国的“不文明史”的时候了。<br />
<br />
&nbsp; &nbsp; 萧建生对中国文明的反思、批判和重新解读，是对近代以来几代中国思想家和改革家的反思成果的综合与提炼。比如，他将秦始皇暴力统一六国、建立皇帝专制的大一统国家看作是历史的巨大退步，他认为秦朝政治结构设计思想是失败的，秦朝的法律严重摧残了人权，大一统扼杀了国际外交竞争、国际贸易竞争和国际人才竞争。他进而指出：“与两千多年的秦朝相比，当今中国的政治并没有本质上的进步，依然是专制、集权、大一统，残酷的丛林法则在中国依然通行无阻。这样一种政治生态导致现代文明生成的可能性不大。”这些看法源于晚清思想家谭嗣同《仁学》中“两千年皆行秦制”的看法。毛泽东最欣赏的古人便是秦始皇，“毛制”即是“秦制”的“青出于蓝”版。再比如，萧建生为“积贫积弱”的宋朝翻案，他认为宋朝是古代中国文明的顶峰，其空前繁荣的商品经济和物质文明，自由开放和积极健康的多元化社会，不仅是汉唐所不及，更是此后的元明清无法比拟的。此观点来自于史学大师陈寅恪，陈寅恪在《邓广铭宋史职官志考证序》一文中指出：“华夏民族之文化，历数千载之演进，造极于赵宋之世。后渐衰微，终必复振。”此种思路是对当今国人“大国崛起”迷梦的当头棒喝：宋朝虽然国土缩小，但民众的生活安定富足，且享有中国历史上“前无古人、后无来者”的自由；而元朝之版图虽然宽阔无边，多数民众却只能在暴政之下呻吟与挣扎。作为一名普通百姓，你愿意生活在宋朝还是元朝呢？你是愿意当“小国富民”还是“大国寡民”呢？<br />
<br />
<br />
六次文明转型为何失败<br />
<br />
<br />
&nbsp; &nbsp; 在我看来，《中国文明的反思》一书中有独创性的部分，是作者对近代中国6次“文明转型”的分析与总结。这6次文明转型分别是：戊戌变法、立宪运动、辛亥革命、五四运动、联省自治和1946年国共谈判。如果将眼光延伸到当代，则还有第7次文明转型的挫败，那就是1989年的六四事件。面对这张“屡战屡败”的成绩单，人们不禁要痛心疾首地追问：为什么在这7次文明转型的关键时刻，中国屡屡错过“天时、地利、人和”的良好契机，反倒是飞蛾扑火般地选择那种最坏的选择？甚至连次坏的那种选择都没有选择？1949年中共建政之后，中国陷入了血雨腥风的暴政的深渊，迄今仍然徘徊于人类文明的主流和普世价值之外，用历史学家唐德刚的说法，中国尚未完成“出三峡”的现代国家之选型。为何中国的转型如此艰难？是地理环境决定论吗？是文化传统有病根吗？还是中国人的人种、基因就不好？上个世纪80历代，知识份子给出的答案有“中国传统文化的超稳定结构说”、“救亡压倒启蒙说”等。而萧建元给出的答案是：“一个没有宗教信仰的民族，不可能走向民主宪政的现代文明；一个不能正确处理宗教问题、信仰问题的国家，是一个不可能享有自由的国家。”以上7次失败，各有其历史背景和环境制约，但7次失败渐次发生在中国，显然并非偶然，它们有一个共同的、根本性原因，那就是：“基督教并没有成为多数中国人的信仰，使近代中国文明转型在‘为达目的，不择手段’的实用主义哲学及‘无毒不丈夫’的暴力文化、流氓文化、阴谋文化等不良文化的指引下而归于失败。”当然，你可以不同意萧建生的这一富于刺激性的论点，但你无法回避他所提出的问题。<br />
<br />
&nbsp; &nbsp; 如果用此思路来反思辛亥革命，为什么孙中山与袁世凯都未能成为像华盛顿那样功成身退的、真正“为万世开太平”的人物？孙中山具有相当的西学背景，亦有基督徒的名份，初期也不乏理想主义的热忱；袁世凯则是清帝国体制内最有才华、改革成绩最突出的高级官员，对富国强兵有一定的见地，但他们为什么不能为民国奠定三权分立、宪法至上的制度基石呢？因为主导他们的思维方式和生活方式的，仍然是中国传统的厚黑学、权谋术、成王败寇的历史观和“宁可我负天下人，不可天下人负我”的人生观。他们崇尚幕后操作和暴力，而不愿接受光明正大的选举，不愿遵守誓言、契约和法律，因为他们心灵深处并没有一个真正敬畏的上帝。中国历代之帝王均为盗贼流氓之“黄袍加身”，近代以来之政治人物，孙中山、袁世凯、张作霖、蒋介石、毛泽东、邓小平，在精神气质上均与之一脉相承，他们的美学范式是所谓的“流氓美学”，用萧建生的话来说就是：“这种流氓美学带来的后果，是整个民间社会对血腥暴力的崇拜和对法律的蔑视，对公民意识的抛弃，对民族性格的形成，对民族精神的毒害，起到了难以估量的作用。”只要看一看李志绥笔下的毛泽东和高文谦笔下的周恩来，以及李锐《庐山会议实录》中中共高层会议的场景，你就会感慨说：这不是一群黑帮头子、这不是一个大贼窝又是什么呢？所以，萧建生总结辛亥革命失败的教训时指出：“任何时候都要用和平、法治和改革的手段，去积累文明的成果，去建立民主政治制度和推进中国的现代化；要具有宽容、妥协、谅解的精神，才能达成民主、法治的制度框架；暴力革命当然也可能带来民主和自由，但是如果缺乏政治智慧，处理不当，超过了社会的承受能力，就只会带来新的集权和专制，不可能实现华夏文明转型的目标。”<br />
<br />
<br />
中国为什么没有华盛顿<br />
<br />
<br />
&nbsp; &nbsp; 如果以此思路来反思五四运动，五四之弊端亦暴露无遗。萧建生认为：“陈独秀、胡适等人虽然大力鼓吹向西方学习，可是他们当时并不真正了解西方文明的根本精神。西方文明的根本精神就是对上帝的信仰，是基督教文明，是这种对上帝的信仰奠定了西方现代文明的根基。西方的人权主张、三权分立、地方自治、公平竞争、遵守游戏规则、保护少数人的权利、为竞争失败的对手着想的思想等等，都是源自对上帝的信仰。”胡适等人虽然长期留学西方，却故意忽视基督教对西方文明的影响，只是留连于现代大学中的人本主义的学术知识，从来不到教堂中去寻求上帝的启示。所以，他们未能发现，英美的近代化及宪政化之路，可以说是清教徒群体开创的，“从英国的《自由大宪章》，到美国的《五月花号公约》、《独立宣言》和《宪法》的完成，其中基督信仰是起了决定性的作用的。没有对上帝的信仰，没有权力谦卑的意识，这些现代文明的制度是不可能建立起来的。这就是西方文明的精髓。”五四运动当中，知识份子高举“德先生”与“赛先生”的旗帜，将其当作新的偶像来崇拜，并以为它们是包治百病的良药。五四运动之后，知识份子又掀起“非基督教运动”，左中右各派人士都“你方唱罢我登场”。殊不知，民主与科学根植于基督教信仰的土壤之上，仅仅移植树苗而舍弃土壤，树苗焉能成活？五四一代知识份子，取启蒙主义，弃新教伦理，可谓舍本逐末，再加上缺乏张灏所说的“幽暗意识”，便很自然地、前赴后继地奔向一条比赛谁更激进的赛道，最终导致马克思主义攫取青年的心灵，在最美好的口号下打造出了一个最暴虐、最疯狂的极权主义政权。<br />
<br />
&nbsp; &nbsp; 英国历史学家汤因比说过：“人类的历史，就是人类与上帝的交流史，就是人类对上帝呼唤的回应史。”从东汉以来，基督教几次入华，均昙花一现而未能生根发芽，中国人崇尚“好死不如赖活”、“宁做太平犬，不做乱世人”的实用理性，不愿接受一个超越性的上帝的存在，对上帝的呼唤始终假装没有听到。从这个意义上来说，中国的文明史，实际上是一部“不文明史”。因为离开了上帝，文明便无从谈起。当年，古罗马的民众之所以改信基督教，如16世纪意大利的政治哲学家麦契瓦里所说，是“将其作为维持一个文明国度的最必需的工具”。而自从宗教改革以来，新教伦理成为现代化的精神源泉，从荷兰到英格兰再到美国，一切现代的文明、观念、制度与物质财富，都与清教徒密不可分。美国的开国元勋的主体便是一群信仰虔诚、品格高尚、知识渊博的清教徒。华盛顿说：“研究复杂事物、培养推理能力的良好教育，加上以往的经验，都无法让我们相信排除了宗教原则，国家的道德观可以得到长久的维持。”约翰。亚当斯在给杰斐逊的信中说：“没有宗教，这个世界将成为君子所不齿之地，我指的是地狱。”麦迪逊形容说，宗教信仰自由“是我国的荣耀”。美国强大的根基在哪里呢？社会学家贝拉在具有里程碑意义的论文《美利坚的民众宗教》中指出，“美国是上帝垂青的国度”，“上帝已经带领他的子民，建立了一整套全新的社会秩序，这一制度的光辉将普照全球的每个国家”。美国作家乔恩。米查姆则发现：“不管在历史的危急关头，还是在风平浪静的年代，美国民众都曾呼唤先贤们所信仰的上帝为他们提供保护，祈求他的引导，为能顺利通过暴风骤雨向他感恩。”<br />
<br />
&nbsp; &nbsp; 萧建生用20年时间完成这部《中国文明的反思》，显然不是在从事一种“解剖僵尸”的工作，而是在探求未来的中国如何才能实现“制约政府的权力，保障国人的权利和自由”的“良治”。太史公说过，历史的使命是“述往事、思来者”，今天，反思中国的历史，可谓“前车可鉴、覆辙不蹈”；旁观英美的历史，可谓“他山之石、可以攻玉”。所以，包括萧建生在内的越来越多的中国的有识之士深刻地意识到：“只有上帝的声音是灵魂的粮食，只有基督信仰才能拯救中国人的灵魂。”这是一个谁也无法否认的事实：作为地球上人数最多的一个族群的中国人，在新教进入中国两百年之后，在经历了漫长的背叛、流浪和苦难之后，终于开始倾听福音、接受福音、拥抱福音，并开始着手去创建一个以上帝的公义和爱为根基的“山上的国家”。<br />
<br />
<br />
&nbsp; &nbsp; （2009年11月26日，感恩节）◆</div>

							
							
							
							
