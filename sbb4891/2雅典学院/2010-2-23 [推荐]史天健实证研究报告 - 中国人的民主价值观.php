													<h2>[推荐]史天健实证研究报告 - 中国人的民主价值观</h2>
						
						
																											<div id="postmessage_490345" class="t_msgfont">中国人的民主价值观<br />
2009年04月03日 15:27凤凰周刊【大 中 小】 【打印】 已有评论81条<br />
实证研究报告&nbsp;&nbsp;专访美国杜克大学政治学教授史天健<br />
<br />
□ 文／记者玛雅<br />
<br />
80％多的中国人认为民主比专制好<br />
<br />
玛雅：中国经过30年的改革开放，由一个相对封闭单一的社会转变为开放多元的社会，在经济、政治、社会各方面都发生了巨大变迁。相应的，人的观念形态也出现了大的转变。这些变化，近年来一直备受国外学界、政界的关注。你在美国执教多年，主要研究中国政治，能否从海外观察的视角谈谈中国的民主政治发展，以及人的政治价值观的变化？<br />
<br />
史天健：民主是大家都关心的一个问题。中国的民主到底是一个什么状况？我从1990年代前期开始做了很多实证研究，其中规模最大的一次是在2002年。<br />
<br />
1991年苏联东欧剧变后，美国学界出了一本非常有名的书，叫《历史的终结》。讲的是由于苏东的倒台，资本主义和社会主义、民主自由和集权专制之间的争论，以民主的胜利宣告了历史的终结。在这个大的背景下，新加坡总理李光耀和美国《外交》季刊一位编辑在1994年有一个访谈，对这个所谓的历史终结，提出了不同看法。李光耀认为，亚洲文化和西方文化有着相当大的不同，而亚洲文化对于亚洲的政体和亚洲各国的政治发展，会产生相当大的影响。这个谈话在全世界引起了非常大的反响和争论，中国国内也有一个比较大的争论。争论的主要问题是，亚洲文化和西方文化有没有什么不同？如果不同，亚洲文化对于亚洲的民主发展有什么影响？<br />
<br />
玛雅：这场争论有什么结果？你个人对这个问题怎么看？<br />
<br />
史天健：到目前为止，双方的争论主要是用理论推导来进行的，在很多情况下，没有实证研究的支持。究竟所谓亚洲的民主价值观理论有没有什么根据？2002年我们在中国做了一个调查，从调查的结果中，我们发现了一个比较奇怪的现象。<br />
<br />
我们问中国老百姓，下面这些说法哪一个符合你的看法：民主体制总是比其他政治体制好；在有些情况下，一个专制的政府比民主的政府要好；民主体制也好，专制体制也好，两者都一样？回答的结果是，超过80％的人认为，民主体制比专制体制好，民主体制适合自己的国家。而且从平均数来看，在被调查的亚洲5个国家和台湾、香港地区中，中国大陆的老百姓对民主的认同高于任何其他国家和地区。<br />
<br />
玛雅：这是不是说，大多数中国人认为，应该实行民主政治？<br />
<br />
史天健：如果研究只做到这儿，答案自然是肯定的。但这样的研究并不严谨，留下一个“被遗忘的角落”。因为这样的研究，只了解到人民对民主的需求，而没有了解“民主的供给”，就是大家认为民主好，那现在我们国家到底有没有民主呢？为了发现答案，我们设计了另一个问题：如果1代表完全不民主，10代表完全民主，你认为你的国家和地区目前民主发展的程度怎么样？<br />
<br />
我们问了1987、1995和2002年这三个不同时间中国大陆的民主程度，发现1987年的时候，平均数是三点几；到了1995年，平均数变成五点几了；而到了2002年，平均数就变成七点几了。从这个回答中我们发现，中国老百姓是需要民主的。但同时认为，中国现在民主的供给相当高。在亚洲5国和两个地区中排第二。<br />
<br />
一个社会对于民主的要求，并非简单地取决于人民的民主需求，而是取决于供求之间的差距。如果我们看一下民主的供求差距，我们发现，中国的民主供给和民主需求的差距，并不像人们想象的那样，好像是非常大的。另外一个非常有意思的发现是，在上述国家和地区中，只有一个地方，就是台湾，人们觉得民主的供给超过了需求，民主太多了。<br />
<br />
玛雅：这个结果可能出乎很多人的意料，你对这样一个结果如何解读？<br />
<br />
史天健：为什么中国人对本国民主供给的评价，高于多数亚洲国家和地区人民的评价？为什么中国民主的供求矛盾并不像我们想象的那么大？我们有两个理论假说来解释这个现象：第一个是“害怕理论”：因为中国在很多西方人看来不是一个自由的国家，那么在一个所谓非民主的国家做关于民主的调查，老百姓在回答问题的时候可能会害怕，不敢说实话，本来没有民主，他说有民主。<br />
<br />
这个假说是可以检验的，方法是用一个指标体系来衡量老百姓是不是害怕，害怕影响不影响他对这个问题的回答。我们问了一个问题：如果你批评政府，怕不怕被人打小报告？有20％的人说，他还是害怕的。但是一个人批评政府害怕被打小报告，不一定导致他不敢回答中国是否民主。我们有一个非常简单的统计学方法来检验这个问题，就是看一个人对于我国民主供给的评价和害怕之间的相关系数是正的还是负的。如果他的害怕导致不说实话，这个应该是正相关的。就是说，他越害怕，他就越说中国很民主。反之就是负相关，他越害怕，就越应该说中国的民主不够。我们做出来的结果是负相关，说明害怕并不能解释这个问题。<br />
<br />
中国老百姓要的民主是民本主义<br />
<br />
玛雅：也就是说，老百姓对中国的民主供给评价高，还是反映了他们的真实想法？<br />
<br />
史天健：应该是。我们的第二个理论假说是，中国老百姓对民主的定义和西方对民主的定义是不一样的。老百姓对民主的理解不一样，对民主的需求也就不一样。他们所讲的民主和想要的民主，可能和有些知识分子所追求的民主，或者西方意义上的民主有一些区别。<br />
<br />
为了检验这个假说，我们设计了另外一个问题：有些人认为可以改用其它的方法来治理我们的国家，下面列出四种方法，请问您的看法如何？1.让人民透过选举，来决定谁来领导国家；2.废除人大和选举，由专家来决定一切；3.允许不同的党派之间竞争，来决定谁来执政；4.由军队来统治国家。<br />
<br />
我们把这四个问题分为两组，1和3我叫它“向往民主”，2和4叫做“支持独裁”。如果说，反对独裁可能是向往民主的一体两面，那么如果他向往民主，就可以从理论上假定，他一定反对独裁，这两组问题的相关系数就应该是1。但是我们对于中国的数据分析发现，这两组问题的相关系数非常小。<br />
<br />
玛雅：这又是怎么回事？中国老百姓向往民主，但是并不反对独裁？这个似乎说不通。<br />
<br />
史天健：这涉及对民主的理解。理论上，对民主有两种定义：一个是熊彼特或者洛克的程序民主，把人民与政府的关系定义为交换关系，选举是政府唯一的合法性源泉。不同的利益者在政治市场上按照程序进行竞争，政府对其要求以及是否符合程序进行判断。另外一个，在中国有孔孟的民本思想。很多研究者都说，民本思想有民主的因素。但是实际上，民本与民主有两个重要的区别：第一，民本把政府和人民的关系定义为君臣关系；第二，政府的合法性主要在于其政策的优劣，怎么得到的权力并不是非常重要。对政策的是非标准，也不在于是否符合程序，而在于其本质，也就是所谓的实质民主。<br />
<br />
那么中国老百姓对民主的定义是什么？我们问了一个开放题：大家都讲民主，对你来说，民主到底指的是什么？发现只有不到12％的人表达的是程序民主；6.3％的人认为民主是制衡dictator（独裁者）；22.9％的人认为，民主指的就是自由；而将近55％的人则认为，民主是政府在作决策的时候，时刻想着人民的利益，征求和听取人民的意见，政府应该为人民服务。这后一种理解，如果按照西方民主理论，或者程序民主的理论，不能叫民主，这叫无害的威权，benevolentdictator。<br />
<br />
玛雅：这是不是因为，中国老百姓没有经历过西方民主，不知道什么是程序民主？<br />
<br />
史天健：有人会说，你是在一个非民主国家作调查，老百姓对民主的概念和认知可能都是由于制度引起的。因为他没有经历过民主，所以不可能真正理解什么是民主，比如他会认为民主就是自由，而民主绝对包括自由。但是如果制度变了，人民对于民主的理解也会变的。换言之，我们在中国大陆的发现，是由于制度不民主造成的。<br />
<br />
制度原因能不能解释我们的发现呢？因为我们在台湾也问了同样的问题，我们可以用台湾的数据和大陆的数据进行比较。台湾从1987年开始发展民主，到2002年已经15年，时间不算短了，如果两地的结果类似，那就不能把原因归于政治制度。比较的结果显示，台湾人对于民主的认知和大陆基本上是一致。只有不到14％的人认为民主是选举、制衡。有将近五成的人把民主定义为自由。同时，有33.7％的人回答，民主是政府听取人民的意见，为人民服务。换句话说，相当一部分台湾人在讲到民主的时候，也是孔孟的民本思想。什么叫民主？政府想着人民的利益，为人民服务，这就叫民主。<br />
<br />
玛雅：就是共产党说的“权为民所用”。<br />
<br />
史天健：权为民所用。这和西方的程序民主没什么关系。这说明，用制度原因来解释，解释不了。<br />
<br />
顺着这个思路，我们最近又做了一个工作。刚才说了，从民主理论的发展来说，实际上有两大理论：一个是所谓程序民主，一个是所谓实质民主。现在西方以选举为中心的民主，就是从熊彼特理论移植过来的程序民主。它的基本逻辑是，因为每个公民都很忙，没有时间搞政治，所以把治理国家交给一部分专业人士。但是对这些专业人士，人民要保留一个权力，就是如果对他不满意，下次选举就让他下台。另外一个理论，是中国的所谓民本思想，或者实质民主。中国人一向觉得，皇上怎么当上的其实不重要，关键是皇上能不能给大家做好事。你看中国历史上，篡权的皇上，很多都是比较出色的。贞观之治的李世民，是个篡权的皇上，如果按照程序民主，绝对是不应该当领导的。还有明朝的永乐皇帝，就是朱棣，也是篡权的，也是少有的好皇上。换句话说，孔子的政治哲学给我们的启示是，衡量一个统治者的标准，不是看他是怎么上台的，而是他在台上能不能代表人民的利益。<br />
<br />
基于这个考虑，我们就把程序民主和实质民主分开提问。下面两个说法你觉得哪个更重要：第一，党和国家领导人是由有规律的选举产生；第二，党和国家领导人在作决策的时候，时时刻刻想到人民的利益？这二者是必须择一的。结果发现，80%的人都说，程序并不重要，重要的是实质。这是目前中国老百姓民意的一个最基本的情况。<br />
<br />
玛雅：前面你谈到，台湾人认为，台湾的民主供给大于他们的需求。这也是因为他们对民主的理解和西方民主有所不同？<br />
<br />
史天健：就是这个原因。如果老百姓要的是实质民主，你给的是程序民主，老百姓还是会认为你不民主。台湾人觉得台湾民主太多了，是因为他要的没有给他，给他的不是他真正想要的。台湾领导人是选出来的，但是轮流上台搞腐败，不为老百姓谋利，老百姓当然不满意。所以我觉得，中国未来在发展民主的过程中，可能受到两个因素的影响：一是老百姓的政治文化――他所向往的民主，或者他觉得最理想的民主应该是什么样的，这个会影响推进民主的过程。第二个，如果你给他的不是他想要的，他还是不会满意，还会不断地回来找实质民主。<br />
<br />
玛雅：从以上调查中，可以得出什么结论？<br />
<br />
史天健：<strong>结论是说，第一，中国人想要民主，但中国老百姓想要的民主恐怕更符合孔孟的民本思想，而不是西方意义上的民主，尽管他们用“民主”这个字眼来形容他们的理想。第二，人民是以不同的标准在评价政府，而这种评价并非简单的选举就可以满足。第三，对于政府和政治学者来讲，这就提出了一个比较大的问题：我们在作制度设计的时候，要怎么样来发展民主？在什么程度上满足人民的要求？</strong><br />
<br />
制度设计要和政治文化相兼容<br />
<br />
玛雅：从对政府的监督来看，你认为是程序民主效果更好，还是实质民主效果更好？<br />
<br />
史天健：到目前为止，没有任何一个理论告诉我，程序民主就一定更好。因为，如果我们做一个最基本的政治学的assumption（假定），对于任何一个政治人物来说，politicalsurvival（政治生存）是他的最高原则，我必须在这个位置上呆下去。那么如果是程序民主，我只要四年一次去justify（证明）我自己，说下次还应该归我就ok了，所以美国会有像小布什这么失败的总统及其失败的政策。但是在一个实质民主的国家，对党和国家领导人来说，他的每一个行为，都要不断地去justify。这其实就是为什么这次汶川大地震，国家投入这么大的力量救灾。而且从周恩来开始，包括唐山大地震，每一次救灾政府都是全力以赴。为什么呢？就因为在实质民主的国家，领导人只要有一个行为犯了众怒，他的legitimacy（合法性）立刻就失去了。所以，到底是程序民主更好，还是实质民主更好？我觉得，这个问题可以debate（争论），还需要进一步研究，做实证调查。<br />
<br />
现在的问题是，如果老百姓要的是实质民主，怎么才能用制度来保证这种民主？或者说，在领导人没有做到实质民主，做出的决策老百姓不满意的时候，有没有一个渠道可以表达。实际上，即使是选举，中国的选举可能和别人的也不一样。所以，一方面要有制度保障，同时，你的制度设计要和你的政治文化compatible（相兼容）。我觉得，不管是协商式民主，还是其他一些实验，中国都要探索出自己的道路。就是说，我们必须根据自己的政治文化，设计出第三条路来，因为我们要有对权力的监督制衡，但是单单靠选举并不能满足老百姓的要求。<br />
<br />
玛雅：根据你们的研究，你对改革开放以来中国的政治文化，或者人的政治价值观的变迁如何判断？<br />
<br />
史天健：政治文化会不会变迁，变迁的速度有多快，根据我自己从1990年代前期到现在的研究来看，中国的政治文化基本上没有变化。而在同样一个时段内，中国社会、经济、政治都经历了天翻地覆的变化。<br />
<br />
我们国家一些积极推进民主化的人总是想，把制度带进去，给老百姓选举权，每一个人就会利用这个权利，追求自己的利益。但是你看中国农村，改革开放后，原来的人民公社垮了，power（权力）和authority（权威）没了。怎么办呢？一帮聪明人，读Huntington（亨廷顿）读得很多，就认为选举可以解决中国的问题。于是就把选举制度引进农村基层，以为老百姓会欢天喜地去利用这个制度解决他们的问题。但是十几年过去后，我们发现，有的地方选举搞得很好，能够解决很多问题。但是在相当多的地方，选举是没有用的，老百姓仍然回到传统的制度去解决问题，比如宗族。大家有事就去找族长，由他把人动员起来，解决农村公共物品供给的问题，比如农地灌溉，而只要传统的制度能够解决问题，没有人bother（费事）去选举。<br />
<br />
我们的研究发现，从传统制度到新的民主制度，中间要经过一个长期和痛苦的博弈，或者用列宁的话说，有一个transitionalstage（过渡阶段）。那么在这个stage，恐怕不是我们人为可以推动的。因为老百姓的选择很简单，哪个制度work（工作），他就用哪个。可能我们看到的是，政治文化在逐渐、逐渐地向民主发展，但这不是一天两天、一年两年的事，可能需要几十年的时间才能完成。<br />
<br />
玛雅：苏联东欧民主转型遇到的最大困境，就是政治文化的转变。当时西方有政治学家就说，一部宪法可以用六个月的时间写成，而政治文化的变革经过60年的时间，也未必能够完成。<br />
<br />
史天健：所以，即使我们想在中国推进民主化的发展，我们还要回答到底要“推进什么样的民主化”和怎样推进的问题，因为中国的民本主义在某种程度上，就是中国人心目中的民主。那为什么我们不去完善我国传统的民本发展？假定我们想要推进西方式的民主，我们要回答的问题是，西方民主有没有可能在中国的政治文化中生根发芽？我觉得，我们应当仔细研究中国传统的制度和新的制度之间是如何interact（互动），需要创作什么样的条件，让新的制度在我们的社会土壤中逐渐生长。而不是幻想，我明天把一个新的制度引入中国，这个制度就会帮助我们达到所要达到的目的。那样的话，可能会把整个事情搞糟。<br />
<br />
很多人看到中国的问题，以一个非常简单的两分法来判断，中国的一切问题都是因为不民主造成的，只要引进民主制度，就可以解决我们国家面临的大部分问题。然而，简单地引进民主真的能解决中国的问题吗？我想，即使我们想要在中国推进西方式的民主，也要塌下心来认真地研究一下，它的问题在哪里，我们要对其作一些什么样的改善，才能使其在我们国家发展。<br />
<br />
