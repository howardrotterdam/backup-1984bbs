<strong>转载者按：共转载过竖鸡的两篇文章，那一篇叫”哥们，妞和我们无关“，豆瓣上的,不过后来他删掉了，但是我右键了…<br />
每逢看到这样文字，总是想站起来和作者握握手。<br />
最后那一理头的温柔，像一朵水莲花不甚凉风的娇羞，道一声珍重，道一声珍重。<br />
我也来模仿一下：对了，我一直很想看竖鸡长啥熊样，但未遂。而今，原来Google尹相杰就可以了</strong><br />
<br />
很是想念凤凰网的老友们，虽然只过去了半个月。我本想追个点名，挨个回忆，但想想又觉得不宜把太多隐私公布出来。毕竟，我成了一个“被重点盯防的异议人士”，这种待遇或会成为老友们的一种“累赘”。如一位大眼儿君被我问及是否会去铁牢送一份凉饺子，他严肃到一声不吭。感谢DQ这几年在我耳边循环播报卡内基的人性学；感谢LJ、ZJ、GL、CL、LMZ、XY、WJ、YYX、HS这一小撮铁兄妹与我共渡醉生梦死的无数良宵；感谢Blog组对我的宽容和支持，如两位同事对我说“16+”是“最美好的时光” ；感谢研发组LLS、LXL，我工作中的风光与虚荣有你们应得的一份；感谢PM组YWL、TS（抱歉，还有几位先行的美丽女士们），工作之余，我们能否聊些养猫百科以外的话题；感谢足球队及其负责人们给我那么多宝贵的上场机会，2008年与北京网管办踢的那场假球令我永生难忘；感谢ZM、WHP、LY对我的信任和包容，是你们给了我拥有这三年美好光景的机会，虽然这结果令人失望，不过合作的机会仍会有；感谢我认识和认识我的所有同事。<br />
<br />
很多人听我总抱怨说“中国不自由”，常会有人反驳“美国也一样”。希望我的离去能给喜欢这样辩驳的同事一个开智的机会。不过这三年里，我认识的同事里，感受到他们更多是对自由渴望和追求，几乎难以遇到在爱国主义和民族主义粪池里畅游的怪人。<br />
<br />
我追求自由，但一直浑浑噩噩。直到今夏被国保约谈后才悟出什么才是真正的自由： 免于恐惧和不虞匮乏的自由。上周，我和美国大使馆政务处的朋友午餐聊到我的遭遇和“什么是自由”时，他用蹩脚的中文告诉我：只有在中国生活时间长了才感受到美国宪法里是如何的保障公民的自由，自由是多么的重要，过去在美国十几年都没有感觉到这么深刻。<br />
<br />
9月份我会去一次公司，办理手续，希望到时候见面。<br />
<br />
最近我的生活还算充实：我将一直没空闲时间阅读的一套丛书读完、重温了一下我最爱看的美剧之一《office》、观看一些导演的作品集、偶尔做些英语题背些单词、作为业余厨子半个月里她下班后没吃过重样的晚餐、养花喂猫逛书店、游走菜市场和超市、晨跑（只坚持两天）、周末为1984bbs足球队效力。我觉得我放松的差不多了，接下来将继续运营好1984bbs。<br />
<br />
接下来的计划：我将成为1984bbs第一名正式的员工；9月中旬到11月中旬我将去北外上托福的课；12月考虑去澳洲看朋友。<br />
<br />
对了，我理了头短发，这是我一直想做的事情――但总是有“恐惧”，而今，我总算已免于部分恐惧。<br />
