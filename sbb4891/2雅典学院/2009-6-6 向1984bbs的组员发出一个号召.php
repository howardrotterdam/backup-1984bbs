													<h2>想象中的动物――水差子的悲鸣</h2>
						
						
																											<div id="postmessage_350114" class="t_msgfont">作者：徐来<br />
<br />
北印度菩提伽耶寺院遗址出土的一些经卷中记录了很多有关动物的故事。其中的一个由见习僧人那连提黎耶舍记录而成。故事大意转述如下。因为原文以吐火罗文撰写，夹杂了大量古梵文和巴利文短语，部分翻译或许并不精确：<br />
<br />
我曾经游历东印度诸学府，寻求突破执著的技巧。游学期间，我一度挂单于迦湿弥罗的纳兰达寺，在此向号称“精进第一”的萨遮迦大师求教。<br />
<br />
当地淫雨已经超过十天，空气中弥漫着泥土的腥味，地面上到处都是积水，泡得发白的蚯蚓被水流冲击着，放肆地扭动，飘过僧人的脚面。<br />
<br />
寺的中庭里，有一个芦席搭成的棚。萨遮迦大师常年在这里沉思。<br />
<br />
那是秋天，一个清冷的夜晚，我带着积蓄多年的疑惑走进席棚，准备从大师那里寻求解答。<br />
<br />
这席棚不知已经搭了多少年，本地湿润的气候将它腐蚀得千疮百孔。棚外的雨水穿过孔隙，以各种角度滴在大师脸上。大师衰老的脸沟壑纵横，褶皱里聚集了各式各样的雨滴，有些是普通的滴形，有些是六角形，有些则形如贝叶。我抬手准备帮他擦拭，他却突然开了口。<br />
<br />
他问我，是否知道南方的僧迦罗国出产名为“水差子”的虫。我摇摇头，发现大师始终闭着眼睛，又小声接了一句“不”。大师说自己就是僧迦罗人。他告诉我，多年以前也是秋天，自己发了一个愿，从此移居到庭院的席棚里。当晚如同今晚一样多雨而寒冷，他在一片寂静中听到附近池塘里，有水差子在呼喊。<br />
<br />
萨遮迦大师说，水差子们聚集在一起，正谈论自己生活的世界，池塘里装满了水，水的顶端是“天穹”，天穹的外面又是什么呢？其中的一个稍年长一些，他告诉其他同类：<br />
<br />
关于天穹外面的世界，想必你们已经听到了很多说法。其中一些来自青蛙，一些来自，另一些则来自芋螺。这些说法都被记录在水差子的经典中，以便我们中的任何一个随时翻查。我们都曾读过这些。我们也都不曾看到过真相。<br />
<br />
接着，这只水差子回忆了自己的年轻时代：<br />
<br />
长到一定岁数之后，水差子的皮肤就会日渐粗糙，透出棕灰色。这说明他就快要变成一颗粗糙的蛹，冲破天穹进入另一个世界了。这么多年来，我眼看着自己的朋友一个又一个穿过了天穹。他们每一个都向我承诺，自己必将想尽一切办法，回到池塘中来，好告诉我天穹之外的世界究竟是什么样的――以证明或者否定，那些记录在历史典籍里的传言。但没人回来过，一个也没有。很明显，没人愿意回来，跟留下来的同类谈谈头顶上那深不可测的未来。<br />
<br />
这只水差子叹了一口气，最后说：<br />
<br />
现在，我也活得足够长了。我就要离开池塘，进入那个新世界了。请相信，朋友们，我不会如其他水差子一样，一去不回。<br />
<br />
说完，他后足一蹬，慢慢浮起。穿过“天穹”的一瞬间，他的身体发生了巨大的变化。一条细长轻盈的绿色身体取代了他原本棕灰色短粗的躯干，背部隆起的肿块舒张成透明细长的翅膀。那翅膀收集住周围微弱的光线，五彩斑斓。水差子终于变成了一种美丽的动物。它只属于天穹以外的世界。<br />
<br />
他在空中飞了一道弧线，然后俯冲向水面，以便冲破水的封锁，回到曾经栖身的池塘，和依然留在那里的水差子见面。但无论如何努力，他都无法冲破那层天穹，回到他逃离的那一面。现在，他惟一能做的事情，只是将尾部插入水中，但这没有任何用处。留在池塘底的水差子们依然不知道外面世界的情况，也不知道那些已经离开的同类，在穿越天穹时究竟发生了什么。<br />
<br />
剩下的水差子依然安静地躺在池塘底部，仰望天穹，那里有青白色的光透进来，有时能看到一两个黑影掠过。不断有水差子从群体中升起，漂向水面。留下的则只能发出震耳欲聋的悲鸣：<br />
<br />
所有的山冈都被翻越<br />
所有的酒杯都被喝空了<br />
所有优柔寡断的情绪都被抛弃<br />
你们已洞悉一切<br />
难道不能怜悯留在这里的可怜人<br />
说出这个世界的秘密？<br />
<br />
念完诗句，大师突然不再说话，又一次陷入沉思。<br />
<br />
我有些犹豫，不知应当退下，还是将他唤醒，与我对谈。正在我犹豫不决的时候，萨遮迦大师突然发出撕心裂肺的惨叫。他头顶的皮肤发出“噶吱噶吱”的声音。很快，一道裂缝撕开了他光滑的头皮，缝隙中露出青绿色的光。裂缝越张越大，从大师面颊两侧翻了下去，还没等我反应过来，一头翠绿的巨大虫子从大师的身体里钻了出来。大师的躯壳像布袋一样摊软在地。而那虫子则抖了抖修长的半透明翅膀，翘着尾巴飞起来，消失在雨水淋漓的、冰冷的天空里。</div>

							
							
							
							
