<br />
　　哈维尔很少研究和分析前期极权主义社会的结构和运作情况。在那个时期，生活的逻辑是“我思想，所以我存在；我开腔，我一定灭亡。”例子多到不胜枚举，布哈林、季诺维也夫还有哈维尔本国的斯兰斯基都是的。哈维尔应该能够认识到，从那个时代转变到人们多少可以争取“说真话”，“从小处着手”，桥下流过的水（不，是血）已经很多了。既然历史已经走到后期极权主义社会，那么也就可以套用中国人十分熟悉的雪莱的诗句：“如果冬天已经到来，春天还会远吗？” <br />
<br />
　　人们说真话，动因是什么？哈维尔回答说：“良心”。 <br />
<br />
