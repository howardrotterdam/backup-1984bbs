日本内阁官房长官相当于政府副首相或发言人。在首相不能行使首相职务5天以上时，代理首相职位，在日本政府中担任着重要的角色。 内阁官房长官是日本内阁中首相以下最重要的阁僚位置。除了代表政府“颜面”出任发言人，内阁官房长官还承担在内阁其他部门进行协调沟通的重任。<br />
