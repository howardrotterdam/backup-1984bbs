													<h2>党内还是有高人――简评钓鱼岛事件</h2>
						
						
																											<div id="postmessage_846079" class="t_msgfont">木头口琴 : 简单评论钓鱼岛事件：党内还是有高人<br />
从 海纳百川 精华导读 作者：木头口琴<br />
有 1 人喜欢此条目 - 您<br />
<br />
<br />
简单谈谈我的浅见，请金前辈等本区高人批改。<br />
<br />
钓鱼岛事件最终这样解决，确实出乎我的意料。虽然我党向来有在外交上“反败为胜”的精神胜利大法，但这次我党似乎真的是赢了。<br />
<br />
这里说的赢，不是国家利益意义上的。从国家利益来说，钓鱼岛还在日本的牢牢控制下，而且日本通过拘押船长已经实现了实际控制的姿态。但其实我党对这个不是很在乎的。一来钓鱼岛不在中国手里已经不知道多少年了，本来就不是实际损失；二来我党向来对国家利益这个东西看得不重。秉国同志在美国的“路线交底”还是说了实话的：<br />
<br />
中国副总理王岐山与国务委员戴秉国二十八日（此为2009年7月28日）称赞为期两天的中美战略与经济对话获得圆满成功。……“确保中美关系长期健康稳定地向前发展，很重要一条是相互理解、尊重支持对方，维护自己的核心利益。”戴秉国接着说，中国的核心利益第一是维护基本制度和国家安全，其次是国家主权和领土完整，第三是经济社会的持续稳定发展。<br />
<br />
所谓第一第二第三，了解中国政治常识的人都知道，第一个是核心，剩下两条都是为核心服务的。就好像四项基本原则，核心是坚持党的领导，这条是铁打的，含义明确的，不能变通的，而剩下三条都是为这一条服务的，对外是可以任意解释的。<br />
<br />
所以，钓鱼岛这件事出来之后，最关键的不是解决钓鱼岛本身的问题，而是如何尽快平息――或者用中国语境里的一个特殊词语，“摆平”――由此引发的国内政治危机。这是最关键的。可以设想一个场景：如果没有信息公开的条件，日本悄悄抓了人，悄悄履行了法律程序，那我党是乐于不管这件事的――就好像我党曾经乐于不管香港是不是最终要回归，以及乐于不管俄罗斯是不是用500发炮弹打沉中国船一样。但现在的条件不是这样的，钓鱼岛是个牵扯日本外加两岸三地的大事，是一定要上新闻，也一定要被国内老百姓知道的，而且确实已经知道了，那怎么尽快摆平，就是大问题了。<br />
<br />
而这次钓鱼岛事件之所以闹的这么大，中国这方面的原因就是目前中国正处于一个极度混乱，国内矛盾极度激化，而局势极度不明朗的时刻。钓鱼岛很容易引发难以解决的政治危机（这个下一段会稍微详细解释一下），最后会出现什么结果实难预料。所以我倾向于认为在华尔街日报中文版工作的日本人加藤嘉一泄露的中国外交部人士的忧虑是真的：“中国外交部的有关人士对我说，‘我们没想到形势这么严峻，刚开始判断有疏忽，千万不能造成不可收拾的局面。……’”<br />
<br />
那么为什么说钓鱼岛可能是难以解决的政治危机呢？因为钓鱼岛这件事，有一个地方和六四啊，冯正虎这些事差不多的，就是基本属于我党自己将死自己，连转个小弯都不是很容易。比如六四，我党天天说自己是人民政府人民党人民军队，天天说对方是一小撮，结果对方一下子弄出上百万各界群众上街，把“人民”的架势一摆出来，我党就被“将得死死的”（芦老语），百口莫辩，最后没办法只能大开杀戒，然后永远不提。冯正虎就更不用说了，我党天天说自己要搞法制，说的频率快赶上随便、王司令等网络法家了，于是冯正虎就用合法手段在日本机场轻轻一将，我党就陷入了无语的窘境，真的没办法解套，估计芦老去当胡总温总的智囊也不行。<br />
<br />
钓鱼岛也是这样。我党天天拿民族主义当论证政权合法性，结果日本抓了船长相当于将了我党一军，这真没办法解决。不表态肯定不行，抗议也没用，想硬一下吧，又生怕露了外强中干的底子。所以钓鱼岛事件刚出来的时候，我党真是急得抓耳挠腮。国内那些各怀鬼胎的“坏蛋”更不可能放弃这个大好的将军的机会，纷纷出来说风凉话、搞串联，再不就是或真或假的弄些爱国运动，眼看着就要镇不住了。<br />
<br />
我党对这一幕是相当防备的，因为我党以前就没少拿沈崇事件这种事来将蒋介石的军。蒋介石被将的没办法，只能一遍又一遍出来号召大家“保持冷静”。但“保持冷静”这种话，越说老百姓越不高兴。最后只能引发“爱国狂潮”，把自己生生放在五四运动时候的北洋政府的位置上。<br />
<br />
我个人对钓鱼岛究竟归谁一点兴趣也没有，只是抱着看眼的不怕乱子大的心情，看看这场好戏会如何收场。同时我也站在我党的角度，帮他们想应该怎么圆这件事。但无奈我政治智慧还是不行，能想到的也就是日本最终放人以后，我党可以把它解释成是因为受到我们的压力而放的，或者私下拿着支票本去趟日本，采取一贯的 “把西方买服”的政策，让日本早点放人，给个面子。最后下次注意对类似新闻尽早采取管制措施。其它的我也想不出什么了。<br />
<br />
所以抓四个日本人这招一出来，我确实震惊了。我党真不是盖的，不全是酒囊饭袋，关键时刻还是有人。这招有这么几个好处：<br />
<br />
一，有效督促了日本放人。<br />
二，给了日本放人一个下台的台阶。<br />
三，对国内展示了自己的强硬。（在国内的概念里，对外强硬就是不讲理。）<br />
四，对自己的国际形象损失并不大。<br />
<br />
这招一出来，钓鱼岛危机被迅速扭转，我党这次真的赢了。在民族主义这一点上，不但没有失分，反而得分了。所以我的第一个观感是：党内还是有高人，我党虽然最近有点灰头土脸，但气数还远远不到尽的时候。<br />
<br />
我想这次钓鱼岛事件可能还有个副效应：又增加了一个不能搞政治体制改革的原因。因为抓四个日本人这招，在民主法治体制下确实使不出来。对国人来说，如果现行体制能如此有效的给列强以“反击”，那就是最重要的，还是不要改的好。<br />
<br />
