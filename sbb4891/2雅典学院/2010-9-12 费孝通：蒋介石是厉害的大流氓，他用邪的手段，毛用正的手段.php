																											<div id="postmessage_825597" class="t_msgfont">蒋介石是厉害的大流氓。他用的手段是邪的一面，毛泽东是用的正的一面<br />
<br />
（来源：广州日报）<br />
<img src="http://hiphotos.baidu.com/%B9%B2%BA%CD%B9%FA%D6%D8%B3%BC/pic/item/86f6dd303870f59c5fdf0e7d.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
<img src="http://news.xinhuanet.com/photo/2005-04/30/xin_172040230180395315540124.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
<img src="http://news.xinhuanet.com/photo/2005-04/30/xin_182040230180310918548125.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　<br />
<img src="http://news.xinhuanet.com/photo/2005-04/29/xin_4520402292033343280988.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　朱：您应该既见过毛泽东，又见过蒋介石？<br />
　　<br />
　　费：我在西柏坡，第一次看到毛泽东，我佩服他。他的诗、词、文章多漂亮啊。见蒋介石是在美国，他请我们吃饭。他问我，费先生读什么书呀？我说我读我这一行的书。他说，你得学点中国东西呀？我说我中国东西不通。我根本看不起他，因为从我大哥开始，都是反国民党思想，反对做官的。我们当时都讲清楚的，不为做官，回来教书，知识分子要还我知识分子本来面目。但我一直到最近退下来，才还我知识分子本来面目。<br />
　　<br />
　&nbsp; &nbsp;朱：对蒋介石个人印象怎样？<br />
　　<br />
　　费：他就是个上海的黄金荣，是个厉害的大流氓。他用低下手段，用的是邪的一面，毛泽东是用的正的一面。孙中山想用邪的一面，没用上。<br />
　　<br />
　　朱：你还在西南见到邓小平。<br />
　　<br />
　　费：他请我们吃饭，开西南政务会议也请我们参加的。我觉得邓小平一直很厉害。当时我们大家的民族观点是强调民族的差别，不要一样待遇，他加了一句“不要强调得太厉害了”。他这点看法还是厉害，当时有一种风气是少数民族自居，他认为要摆稳，汉族还是主体。<br />
　　<br />
　&nbsp; &nbsp;大哭一场，大笑一场， 懂了历史是怎么一回事情<br />
　　<br />
　　朱：20世纪50年代初对知识分子进行思想改造，那个时候你是什么感受？<br />
　　<br />
　　费：我也要改呀，心悦诚服，而且觉得很懂了。我还要做样子出来让大家都这么做，示范性的。后来写《知识分子的早春天气》的时候，那是反过来帮知识分子说了一些心里话，是客观主义。<br />
　　<br />
　　朱：所以把你打成右派，你是很意外的。<br />
　　<br />
　　费：是很意外。我没有想到，我当时红得发紫。所以宣布我是右派时，我大哭了一场，大笑了一场，解决了，我懂了，历史是怎么一回事情。<br />
　　<br />
　　朱：您既然打成右派，毛泽东还说您文章好，怎么不作点指示来改变您的处境？<br />
　　<br />
　　费：他请我到他游泳池旁边谈话（1957年），他说：不要紧，右派有什么关系。我自己戴多少帽子啊。他肯定他自己的话是对的。帽子戴上去，会飞掉的。他真的飞掉好几次，他一直在帽子底下做事情的。<br />
　　<br />
　　朱：您对当时您那一代知识分子评价不高，认为他们都是很浮夸，不脚踏实地。<br />
　<br />
　　费：我对这个队伍看不大起。不是浮夸，是庸俗。因为我是实地调查的人，我接触的事物很多，我附近都是知识分子，我看穿了他们。<br />
　　<br />
　　费孝通：志在富民<br />
　　<br />
　　费孝通（1910~2005） 江苏吴江县人，从小接受新式教育。他的整个家庭都是向新的方向走，父亲曾留学日本，在家乡开办了第一所新式中学，母亲开办了第一所幼儿园。<br />
　　1930年，费孝通进入北京燕京大学社会学系，师从冰心的丈夫吴文藻先生，从此走上了实地调查的社会学道路。<br />
　　<br />
　　结婚108天痛失爱妻<br />
　　<br />
　　在燕京大学，费孝通与王同惠相爱。那时，王同惠正在翻译一个外国人的著作《甘肃土人的婚姻》。中国人最基层的事情竟然要外国人来写，两个年轻人心有不甘。1935年夏天，新婚的费孝通和王同惠开始了广西特种民族考察。当他们准备转往茶山瑶地区调查时，费孝通意外被当地人捕老虎的机关压伤，王同惠去找人帮忙，却落下山涧身亡。此时他们刚刚结婚108天。<br />
　　<br />
　　费孝通回到家乡的开弦弓村养伤。姐姐费达生在这里开办了中国现代史上较早的农民自办的丝厂，费孝通把姐姐称为“中国乡镇企业第一人”。在这里，费孝通开始了他一生中最重要的实地调查，那年，他27岁。<br />
　　<br />
　　27岁开始社会调查<br />
　　<br />
　　1936年，带着在开弦弓村一个多月的调查材料的费孝通踏上了去伦敦经济政治学院求学的路程，他的调查材料一下子就得到了导师马林诺斯基的赏识，在此基础上，费孝通完成他的论文《江村经济》。<br />
　　<br />
　　《江村经济》流传极广，马林诺斯基称它为人类学实地调查的里程碑。同时，它把中国农村发展道路问题带向了世界。从这次调查开始，费孝通将中国城乡发展的道路作为他一生的研究课题，此后的六十年中费孝通曾先后二十多次重返开弦弓村实地调查，他的所有研究的目的都在于富民。<br />
　　<br />
　　“小商品，大市场”<br />
　　<br />
　　上世纪40年代，费孝通去美国访问后归国，以“民主斗士”的形象活跃在当时的历史舞台。1946年7月，李公朴、闻一多被暗杀，费孝通在《这是什么世界》一文中写到：“一个国家怎能使人人都觉得自己随时可以被杀！人类全部历史里从来就没有过这种事。我们现在活在什么样的世界里。”<br />
　　<br />
　　费孝通的第二次学术生命是在上世纪八十年代。七十多岁的他每年都花三分之一的时间去搞调查。我们平时常常听到苏南模式、温州模式、珠江模式这样的词汇，都是由费孝通率先提倡运用的。“小商品，大市场”的口号也出自他手。他所倡导的中国农村走向乡镇企业经济，已成为农村经济改革的成功模式。<br />
　　<br />
　　　　2005年4月24日，费孝通在北京病逝。</div>

							
							
							
							
