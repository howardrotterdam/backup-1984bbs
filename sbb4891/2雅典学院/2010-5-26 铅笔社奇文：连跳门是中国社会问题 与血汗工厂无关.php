													<h2>铅笔社奇文：连跳门是中国社会问题 与血汗工厂无关</h2>
						
						
																											<div id="postmessage_640648" class="t_msgfont">By 鲁克 <br />
富士康“八连跳”，“九连跳”的事件，惹来众多媒体的非议，称其为“血汗工厂”。言下之意，是富士康工厂的劳动强度大，工作超负荷，造成了员工心理的问题。因此自杀，猝死都是血汗工厂惹的祸。 <br />
<br />
真正血汗工厂最大的特点是不自由。例如，缅甸的血汗工厂，政府逼迫工人必须在环境恶劣的制造厂打工，这是一种自愿的，双向选择的劳资关系。可是，富士康并没有逼迫大家来为其打工，也没有规定必须加班。他们员工都是自愿要求加班的，只不过富士康提供了这样的机会。打工仔来这工作，无非是希望尽快地多点赚钱，在他们看来，加班多的厂才是“好厂”，因为“不加班，根本挣不到钱”。 <br />
<br />
《南周》记者调查发现，富士康员工多数是不希望公司放假的。因为花钱不挣钱的日子“很难熬”，什么节日不重要，重要的是收入和积攒财富。靠自己的双手积攒财富，才是他们最大的快乐。员工热爱加班，本身就说明了富士康建立了良好的绩效管理即薪酬激励制度。员工能在这里能多劳多得，公平竞争，不象吃大锅饭的国企，工人懒散，消极怠工。美国通用公司养了一堆不做事的，结果企业玩完了。 <br />
<br />
有竞争的地方必然有压力，每个人的身体状况和心理素质都不一样，富士康员工大都是年轻人，其中一部分人背井离乡后情绪低落，压力承受能力低是正常的。就算自杀和猝死和都富士康有关，也不能立刻得出富士康是血汗工厂，压榨工人这样片面的结论。 <br />
<br />
没有媒体批评日本有血汗工厂，但在日本照样有猝死，跳楼的。日本把“工作狂”的猝死称作Karoshi，每年日本Karoshi的至少75人，多则100人。 <br />
<br />
当人的作息不当，不会管理时间，承受压力的生理素质差，猝死就会发生。中国均瑶集团董事长王均瑶生前应酬多，很少休息结果38岁就夭折了。爱立信的中国总裁杨迈因劳累猝死在跑步机上；而信奉“工作是使命感，也是享受”的英业达副董事长温世仁，最后也是操劳过度累死的。但这些人，包括富士康猝死的工人，都是自愿工作的，以工作为乐，没有人逼迫他们。员工猝死和这家企业是不是血汗工厂没有直接的关系。 <br />
<br />
再说自杀。众所周知，美国拉斯维加斯是“自杀之都”，2005年这个赌城所在州发生了400多起自杀事件，而拉斯维加斯包办了307件。但没有人说那里是“血汗工厂”，那里是享乐者的天堂。与之相邻的犹他州，人的寿命长，却鲜有自杀的事件发生。据说，犹他州的人爱过有信仰的生活，幸福感高，凡事节制，中规中矩。 <br />
<br />
说富士康员工自杀之前，我们先看中国的自杀数据。据factsanddetails.com的记录，中国平均每年有28.7万人自杀，平均每10万人中就有23人自杀。深圳富士康有40万人，8，9人自杀，在比例上并不算多。 <br />
<br />
说自杀比例，虽然显得很冷酷。但我要说明的是，竞争压力并不是富士康的问题，而是整个社会的问题。深圳富士康所在地广东省，平均每年有2万人自杀身亡，超过10万人自杀未遂，而深圳平均每年有2000人自杀。难道，广东全部都是血汗工厂？如果是这样，为什么单单拿台资企业说事？这么多人自杀，最近还有变态杀童案件，说明了中国社会出了问题。 <br />
<br />
到底是什么造成了社会上这么多人的焦虑感和异常的心理压力？现在的中国人在精神世界缺乏追求，这是普遍且不可忽视的状况。没有心灵上的安慰，人们对“穷困”的焦虑必然得不到缓解和释放。据factsanddetails.com的记录，中国自杀更多的是发生在农村，农村自杀的人数是城镇的3倍以上。据他们发现，农村人放弃生命大都因为穷困。穷困造成了幸福感缺失，而在心灵上又缺乏寄托，累积成的压力变成了定时的自杀炸弹。 <br />
<br />
农民工选择来城镇，来富士康工厂，是为了摆脱贫困。因为他们知道有收入，有一定的财富积累，忧患和焦虑，压抑感会逐渐化解。正是因为如此，发展中国家比发达国家自杀高很多，而在经济危机下，轻生寻短见的人立刻会多起来。 <br />
<br />
我们可以假设，富士康在社会压力下，被迫关门，情况会怎样？那些底层工人怎么办？失业后回到农村，他们日子会好过么？下岗后自杀的人很多，只是在和谐社会，政府不愿意公布其中的数字罢了。 <br />
<br />
当然自杀还有其他很多因素。对于年轻人来说，其中感情因素通常是主要的。在富士康跳楼的员工当中，几位女性就是感情因素造成的，并不是血汗工厂压榨出来的。跳楼自杀的卢新对工厂待遇是满意的，他在日记中写到“为了钱，来到了公司……钱还算多”。 <br />
<br />
为什么富士康员工自杀多选择跳楼方式结束生命？而且是连二连三的跳？中间有没有关联性？ <br />
<br />
自杀的方式有很多种。一般在农村是喝农药、上吊、投河，在城市则跳楼、割腕、吃安眠药。富士康的员工选择了更便捷的跳楼，有新闻效应，死后可以引起整个社会关注。特别是媒体死盯的富士康，自杀升级更有轰动效果，死者亲友也很快得知，这算是死者最后给社会的一个遗憾的宣告。 <br />
<br />
在媒体和资讯高度发达的时代，自杀也会成为一种流行。精神世界穷途末路的人，很容易效仿别人自我毁灭的方式，类似羊群效应。所谓羊群效应，是指一种群体模仿心理。比如，香港、台湾、日本，都流行过一阵子烧炭自杀，美国很多人自杀选择去拉斯维加斯跳楼。滥杀幼童案媒体曝光后，类似的惨案一下就多了起来。 <br />
<br />
富士康员工跳楼不止，而且发生越来越密集，我认为媒体也有一部分责任。他们在散播负面消息，激化劳资矛盾，加剧恶化员工焦虑的情绪。媒体在做道德审判的时候，为“连跳”计数，累加自杀事件作为给企业施压的筹码，这完全没有尊重死者及其个体生命的价值。当富士康员工被这些炒作的舆论，被仇恨和悲观情绪感染后，更容易意志消沉，轻生的念头就像传染病一样泛滥，渗透到园区的每个角落。 <br />
<br />
富士康准备了一些预防自杀的机制，但更重要的是要抵制媒体的误导，给员工更多正面的精神激励，加强对他们的心理咨询和培训，疏导和缓解园区内不安和低落的情绪。</div>

							
							
							
							
