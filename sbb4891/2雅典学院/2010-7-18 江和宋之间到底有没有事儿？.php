													<h2>毛泽东和他的“通房大丫头”（组图）</h2>
						
						
																											<div id="postmessage_747701" class="t_msgfont">2010-07-15　 作者：顾晓军 　 <br />
有网友在我的《毛泽东身边的女人们（组图）》一贴后，留下了几个大字：“通房大丫头”。<br />
“通房大丫头”？仔细想想，这话还极准确。<br />
毛泽东，一生为革命奔波、戎马倥偬，无暇顾及自己、顾及亲人，即便个人情感、儿女私情，也多为快餐式的。<br />
毛泽东正式的婚姻，应该只有两次。一次是与杨开慧，另一次即为贺子珍。如果说，把杨开慧称作正夫人或大夫人或老夫人，贺子珍则为少夫人。<br />
毛泽东在延安遇上的美女吴广惠，应该算婚外情。后来的江青，则可以算婚外恋。<br />
那么，婚外情与婚外恋有什么区别呢？前者，比较快、如闪电，类似于如今的一夜情；而后者，则比较缠绵悱恻。<br />
事实上，亦如我的分析。<br />
吴广惠，因属婚外情，没能引发毛泽东铭心刻骨的爱，所以被贺子珍赶走了。而江青，则不然DD贺子珍，不单没有能赶走江青，反而把自己逼走了。<br />
然，贺子珍虽把自己逼走了，但名份却一直没有丢。那么，江青在毛泽东身边算什么呢？应该就算是“通房大丫头”。<br />
尽管，在文革中，江青权重位尊、威风凛凛；但，实际上，她依旧只是个“通房大丫头”。<br />
另，还有毛泽东与章含之、王海容、唐闻生等的传闻。这些算不算“通房大丫头”呢？我就吃不准了。<br />
我顾晓军，能吃准的是DD张玉凤和孟锦云，也是“通房大丫头”。<br />
别看“通房大丫头”只是个“丫头”。因“通房”，此“丫头”就非彼丫头了。据传：有一段时间，权重位尊的江青，就害怕张玉凤、常向她示好。<br />
大约，这也是因为毛泽东不与江青“通房”了；所以，她的地位就降到了“通房”的“大丫头”的之下。<br />
其实，“通房大丫头”也是有真实的爱情的。据传：张玉凤，如今依然坚守如初。<br />
有人批评毛泽东，就说他什么都不好。我顾晓军，就不。我说毛泽东还是有魅力的，据传：唐闻生，如今亦依旧单身。<br />
