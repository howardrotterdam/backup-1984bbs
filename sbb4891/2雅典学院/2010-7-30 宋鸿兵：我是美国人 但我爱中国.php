<br />
　　三百年纵深 揭秘金权家族史<br />
<br />
　　由中华工商联合出版社与北京日知图书联合出版的《货币战争2・金权天下》，以300年的历史纵深，全面阐述欧美主要金融势力集团的形成、发展、排挤、冲突、联合与制衡，系统解析当今世界幕后主宰力量的运作和决策机制，第一次揭开统治世界的“国际银行家族俱乐部”的神秘面纱，铺开了一张迄今为止几乎完全不为国人所知的庞大的国际人脉关系网。<br />
<br />
　　宋鸿兵本人表示，这本书的写法跟第一本一样，还是自然、通俗易懂，因为书写成什么样子完全是取决于读者的。第一本是提出了掌握货币发行权的重要意义，而《货币2》则全面表现了17个金融家族对政府、媒体等方方面面的权力驾驭体系，这正是该书的价值所在。这些都是外国金融家们避而不谈的话题，而宋鸿兵正是要透过这些真相，探讨如果2024年世界货币统一，中国应该何去何从。<br />
<br />
　　三年磨一剑 几乎没有休息日<br />
<br />
　　到场嘉宾对宋鸿兵的新书首发表示祝贺，也对这部比前作信息量多出10倍的“巨制”表示赞叹。中国社科院世界经济政治研究所国际金融室副主任张明说：“宋鸿兵为了写《货币战争2》这本书三年没有休息过，经常眼睛红肿，他几乎每天要阅读5万字的资料，非常的辛苦。”<br />
<br />
　　环球财经出品人任文女士说：“宋鸿兵老师是在工作时间之外的业余时间完成《货币战争2》的写作，如果说全北京只有一个人这三年来，每天都能看着太阳升起，那就是宋鸿兵。”<br />
<br />
　　身为美国国籍 民族主义遭质疑<br />
<br />
　　在媒体提问环节，记者问到“您说自己是一个民族主义者，请问您是不是已经加入美国国籍？”时，宋鸿兵停顿一会，回答“是，但不能否认我依然爱中国”，记者又追问“可当您加入美国国籍的时候，是不是宣誓将永远效忠美国？”宋鸿兵不可置否“当时我在美国政府工作，不得不加美国国籍，你这个问题很尖锐，也很有意思。有很多人，像杨振宁教授，也是美国国籍，但是你不能说他不爱国。我可以用犹太人来举个例子，你不能说犹太人不爱以色列，但他们也热爱美国，这是不矛盾的。”<br />
<br />
　　到会记者问道“当世界单一货币在2024年趋于成熟之际，中国应该如何应对？”之时，宋鸿兵说：“这取决于中国在这个体系中能取得多大的利益。如果中国只是一个小股东，那么我们完全可以在亚洲使用区域性货币，与别的货币鼎立而治。”<br />
<br />
