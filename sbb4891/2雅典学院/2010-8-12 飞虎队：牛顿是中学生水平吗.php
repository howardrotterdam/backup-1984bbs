<br />
另一方面是外在的，不光他不愿意交朋友，也没有人真正想跟牛顿当朋友，结交他的人都是有目的的。人们对他只有敬畏和仰慕，并不真的喜欢他。这道理其实很浅显，绝大部分人都热衷于跟比自己傻的人待着，很少有人愿意在人精的身边衬托自己的二逼。所以好多人都喜欢小动物和小孩子，就是因为这些东西够傻，他们在这些东西身上能获得安全感与主宰感。不少姑娘一见到小猫小狗小人儿都会迫不及待的搂抱，接踵而至的就是很嗲的说好可爱欧~，听得我阴毛都竖起来了。有时候可爱和憨态可掬的潜台词就是弱智。小猴子也很好玩，喜欢的人就少多了，因为猴子机灵到能戏弄人，那些人没有驾驭猴子的自信。同理，喜欢小孩的都是喜欢他们的单纯与缺心眼，在他们眼里，小孩跟小动物没有本质区别，也都是四条腿走路，露着屁眼随时拉撒。如果遇到一个小天才，3岁就会心算三位数乘法或者知道傻逼二字的正确写法，她们一定会骇破了胆。所以那些喜欢养猫狗的女士们别再标榜自己有爱心了，你们其实比谁都缺德。我从不喜欢猫狗，这是因为我敬畏大自然的生灵而不忍戏弄它们；我也不喜欢小孩，因为我把他们当作一个大写的人而不是小畜生看待。 <br />
<br />
大家不喜欢牛顿的另一个原因是他性格暴戾乖张。长年在他身边的人回忆说，牛顿在人前只笑过两回，其中一次还是嘲笑：有人问他，欧几里得的《几何原本》那么老朽，不知道还有什么价值。牛顿闻听放声大笑。而且他人品太差，跟谁都打架。众所周知他从小就有校园暴力的记录，胖子同学不小心踩了他的风车，他抬手就把胖子打哭了，我们的教科书居然说这是他有志气的表现。长大了不以拳脚论高下，他就雇用枪手大骂莱布尼茨，甚至不惜化名亲自去骂，人品至此真是无以复加。莱布尼茨若不是脸皮厚早就跟纳什一样疯了，而且牛顿老师肯定会拍个片子叫《丑陋心灵》继续恶心人家。 <br />
<br />
关于牛顿的另一个谎言是他的谦虚，证据就是牛顿老师说过两段著名的话，一段是站在巨人肩膀上，另一段是海边捡石头子。这确实很有迷惑性，我第一次听到后感动的直冒鼻涕泡。但任何话语都是有语境的，巨人肩膀那一句的语境是这样的：胡克其实早就发现了万有引力定律并推导出了正确的公式，但由于数学不好，他只能勉强解释行星绕日的圆周运动，而且他没有认识到支配天体运行的力量其实是普遍存在的，是“万有”的。第谷早在100年前就发现了行星的公转其实是椭圆运动，开普勒甚至提出了行星运动三定律。所以科学界对胡克的成果不太重视。后来数学小狂人牛顿用微积分极其圆满的解决了这个问题并把他提出的力学三条基本定律成功推广到了星系空间，改变了自从亚里士多德以来公认的天地不一的旧观点，被科学界奉为伟大的发现。于是胡克大怒，指责牛顿剽窃了自己的成果。牛顿尖酸刻薄的回敬道：是啊，我他妈还真是站在巨人的肩膀上呢！这本是一句反语，至少不是真的想客气一下。几百年后罗永浩说自己只是站在巨人的肩膀上也是这意思。但后人出于塑造完人的目的，只保留了孤立的原话而去掉了语境，变成了一句彻头彻尾的谦辞。同样的情况出现在另一段话上：牛顿晚年因为树敌过多，来自欧洲大陆比如法德的一些新锐科学家质问他：“牛顿你丫牛逼什么啊？”牛顿此时完全的展现了他科学界大宗师的风度与水平，潇洒的回敬道：“我没有什么牛逼的。我只是一个在海边独自玩耍的小孩，偶尔会为捡到几个美丽的贝壳而欣喜若狂，却对面前浩瀚的真理大海无所察觉。”意思是说你们他妈的连贝壳都看不见有什么资格评价我？几十年的官场毕竟不是白混的，牛老师甩片儿汤话的水平已经到了信手拈来闲庭信步宠辱不惊的境界，所以我们只记住了这一段优美至极、深邃如同诗歌的话语。 <br />
<br />
牛顿老师人品差，不谦虚，没朋友，按现在的说法这是典型的高智商低情商，事业不会成功。但我们也发现，当智商高到一定程度的时候是可以取代情商的。所以那些说自己情商低的所谓天才们，你们没成功只是他妈的还不够聪明而已，怨不着人家情商。要知道牛顿是个遗腹子和早产儿，出生时体重不到5斤，没吃过DHA和RHA配方的奶粉。亲娘改嫁后跟文盲姥姥度过无聊的童年，没有任何的早期智力开发和学前启蒙，7岁上学以前脑子里空空如也，牛妈妈对他的期望仅仅是认识点字然后回家务农。但是牛顿上中学后已经熟练掌握了拉丁语希腊语西班牙语和英语，然后被推荐进了剑桥，20出头就当了卢卡斯教席的终身教授。如果他能活到今天，我一定会请他当熊博网的形象代言人兼吉祥物。 <br />
<br />
晚年的牛顿除了升官发财再无其他骄傲之处，而且官迷心窍，没退休一直干到85岁寿终。当然他并没闲着，写了150万字的神学著作，同时一心扑在化学事业上，在家里盖了窑子，拿出年轻时搞物理的劲头玩命试验。但这次他的出发点就错了，总是希望从黄铜和煤渣中提炼出黄金。要知道化学反应只能改变分子并不能改变原子，能给原子做变性手术的只能是核反应。他违背了化学定律里的物质不灭原则，所以虾米了。 <br />
<br />
最后，说两段悼词。一段是他的墓志铭：伊萨克牛顿爵士，安葬在这里。他以超乎常人的智力，第一个证明了行星的运动与形状；彗星轨道与海洋的潮汐。他孜孜不倦地研究，光线的各种不同的折射角，颜色所产生的种种性质。让人类欢呼，曾经存在过这样一位，伟大的人类之光。另一段是英国诗人写的：自然和自然的规律隐藏在茫茫黑夜之中。上帝说：让牛顿降生吧。于是一片光明。 <br />
<br />
不知道为什么，这两段总是让我有点感动。</div>

							
							
							
							
