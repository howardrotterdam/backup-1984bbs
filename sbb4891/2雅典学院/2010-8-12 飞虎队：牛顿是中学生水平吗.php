													<h2>飞虎队：牛顿是中学生水平吗</h2>
						
						
																											<div id="postmessage_780051" class="t_msgfont">一。 <br />
<br />
爱因斯坦虽然名气很大，不过很大程度上是由于他提出的相对论太过惊世骇俗，他个人生活又极富传奇色彩，所以很吸引眼球。但是要较真的话，我觉得爱因斯坦在科学史上的地位是怎么都无法跟牛顿比的。 <br />
<br />
牛顿才是人类历史上最伟大的科学家。不仅因为他创立的牛顿力学奠定了整个物理学乃至可以说是整个现代科学的基础，而且他还在数学，光学，天文学等几个领域作出了同样不亚于他的牛顿力学的开创性科学贡献。 <br />
<br />
比如说我们在大学学习的高数课程，就是以他创立的微积分为主要内容，而且他仅仅只是为了证明他的牛顿力学，需要一个数学工具，就顺手发明了微积分（虽然莱布尼兹也独立发明了，但是显然没有他那么举重若轻），可想而知，他有多么天才！ <br />
<br />
还有光学方面，好像用光的粒子说来解释衍射折射等现象就是他提出来的（光的粒子说与波动说都是在一定意义上的正确理论），发现了太阳光谱；天文学方面，他提出了万有引力定律。 <br />
<br />
实际上，我还在读书的时候就发现：我们在中学学习的数理化，乃至到了大学学习的高数，高等物理等等公共课中，大部分内容，特别是中学数理化课程简直可以说绝大部分内容，都是西方在几百年前就已经创造出来并已经过实践检验而发展成熟了的科学理论和知识。比如说高数，力学，无机化学，等等。其它的学科最晚也是西方几十年前的成果。 <br />
<br />
说到牛顿，某些Chinese有种愚蠢透顶的认识：认为牛顿的贡献仅仅就只是提出了中学课本上为了便于那些智力还未发育成熟的中学生能够理解而高度概括后的那简明的几条牛顿运动三定律和万有引力定律，说牛顿的科学水平只不过相当于现在的一个中学生。 <br />
<br />
呵呵，真是让人笑掉大牙，我真的搞不懂某些Chinese怎么会这么蠢，你没本事创造出任何的科学成果，人家给你整个地创造出来了，你不虚心学习，还象个文盲一样自以为是地贬低人家说“没什么了不起的，你一说我还不是就懂了”。 <br />
<br />
牛顿的贡献仅仅就只是提出了经高度概括后那简明的几条牛顿运动三定律和万有引力定律那么简单吗？当然不是，人家那是经过了大量的实验观察，严谨的逻辑推理，复杂的数学计算，等等一系列的完整的论证工作，最后得出了一个博大精深，而且系统化的理论体系，并为了便于普通的学生和工程技术人员理解和运用，从而高度概括为这么一个简明的定则，然后人家把这个最终答案直接告诉你，这个时候就总有一些愚蠢的Chinese自鸣得意地说：“原来这么简单啊，我也能想到的”，但是人家只告诉你答案不告诉你证明过程的话，你是永远都不可能自己凭空想出答案的，也永远也没本事自己独立推算证明出来。 <br />
<br />
为了论证这个简明的牛顿运动三定律和万有引力定律，牛顿还不得不先发明了微积分作为数学工具，并在伽利略等前人所作的工作基础上，经过大量的实验观察，逻辑推理，数学计算，写了厚厚一砖头的《自然哲学的数学原理》（其实这本书，一般的China“学者”根本看不懂）来作出了论证，然后这个牛顿力学才成其为科学，不然就只是没有任何实际意义的空想，假想。 <br />
<br />
当然，后来爱因斯坦的相对论对牛顿力学在高速运动的情况下作了修正（而不是否定），但是牛顿力学是在人类能够观察到的低速运动的情况下提出的，在这种情况下，它仍然是适用的，仍然是正确的科学理论。 <br />
<br />
说牛顿的科学水平仅仅只是现在一个中学生的水平，这是某些Chinese发明出来的又一个愚蠢透顶的笑话。可以负责任地说，牛顿的水平，即使放到今天China的任何一所大学，他要胜任一个教授的职位都是绰绰有余的，甚至可以说，牛顿只要花点时间对现在比较前沿的科学动态稍加了解，熟悉。他要胜任China大学里的博士生导师的职位都是轻而易举的事情。 <br />
<br />
难道不是这样的吗？China大学的学术水平怎么样我们大家都应该清楚吧。China大学里滥竽充数，不学无术，甚至读了一辈子书却连最基本的科学原理都没真正搞明白的所谓“教授”“博导”还少了吗？牛顿要跟他们比起来，那才可以说这些China“学者”才真的是“中学生的水平”。 <br />
<br />
为什么很多Chinese对牛顿的科学成就会有这么愚蠢荒唐的看法呢？这跟Chinese颠倒错乱的思维方式有关。我们知道，Chinese的思维方式自古以来就是一种非理性的，无逻辑的，什么事情都是以一种无因果联系的类比法，联想法来推想，比如什么“天无二日，国无二主”，还有如中医用阴阳五行来解释病因。理论和事实之间，现象和结论之间，根本不需要证明的，看着象那么回事，灵机一动，就跳过去直接等于了。 <br />
<br />
所以他们认为别人也是跟他们一样思考问题的。 <br />
<br />
所以才会有某些愚蠢的Chinese一直认为：牛顿真的是在苹果树下被苹果砸了一下头，才灵机一动，就这么凭空想出了牛顿力学。 <br />
<br />
二。 <br />
<br />
其实，这是一个通常很少被人们意识到的，令人惊奇的事实：人类历史上，很多领域中，最伟大最优秀的天才，几乎都出现在盎格鲁萨克逊民族中。如：最伟大的科学家――牛顿；最伟大的生物学家――达尔文；最伟大的文学家之一，莎士比亚；最伟大的经济学家之一，亚当斯密；最伟大的哲学家，思想家之一，培根；最伟大的化学家之一：波义耳。 <br />
<br />
还有其他很多领域的天才人物如：物理学家霍金；发明蒸气机的瓦特；发明青霉素的弗莱明；发现血液循环的哈维；发现电磁感应定律的法拉第；提出电磁场方程以及电磁波的麦克斯韦；创立现代原子论的道尔顿；发现电子的汤姆逊；发明蒸气机车（也就是火车）的Trevithick；电子计算机的鼻祖――Babbage；发明电视的Baird；发现热力学第一定律的焦耳；发现热力学第二定律的开尔文；发明二极管的Fleming；现代医学护理制度的创始人, 红十字会创办人南丁格尔女士；发明牛痘术的Jenner；发明内燃机的Cecil；在显微镜下发现细胞的胡克；还有氢气，氮气，氧气的发现者； <br />
<br />
诗人雪莱，拜伦，济慈；文学家弥尔顿，狄更斯；提出三权分立理论的洛克。 <br />
<br />
以及虽然不是英国人但仍属盎格鲁撒克逊民族的美国人如：美国大发明家爱迪生，发明飞机的莱特兄弟，发明电报的莫尔斯，发明电话的贝尔，还有大科学家富兰克林，以及发现DNA双螺旋结构的几位英美科学家；遗传学家摩尔根；发明三极管的Shockley，发明晶体管的Brattain；发明电子计算机的Eckert。 <br />
<br />
简直举不胜举。 <br />
<br />
而且，英美文化模式，以及英美人创造出来的科学成果，对于我们现代生活中的各个方面：科技，政治，文艺，衣食住行，几乎可以说是产生了决定性的影响，几乎可以说是奠定了我们现代社会的整个基础。 <br />
<br />
