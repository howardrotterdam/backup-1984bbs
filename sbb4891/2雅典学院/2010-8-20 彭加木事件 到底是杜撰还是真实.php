　　本报武汉9月5日电 记者张志峰报道：“干涸多年的罗布泊发现了地下含水带。”中国地质大学副教授李振宇4日在武汉透露了这一消息。<br />
　　 被称为“死亡之湖”的罗布泊，位于新疆塔克拉玛干大沙漠东部，曾是一个内陆湖，后来逐渐干涸，成为一片沙漠。今年7月中旬，李振宇带着地面核磁共振器械，应邀赴罗布泊为当地寻找工业用水源。他和来自新疆的专家发现，（注意）在阿尔金山北缘向罗布泊方向，有一条地下含水带，长10多公里，宽至少4公里，水层最厚处超过50米。（这句被加大了字体并标为红色）据检测，此水带含盐量不高，可用于灌溉。至于处理后能否作人畜饮水，则有待进一步检测。<br />
<br />
