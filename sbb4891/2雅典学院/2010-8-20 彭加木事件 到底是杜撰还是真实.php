													<h2>彭加木事件 到底是杜撰还是真实</h2>
						
						
																											<div id="postmessage_790092" class="t_msgfont">这是校内上一篇日志，转来求真相<br />
“双鱼玉佩”、一个关于中国科学院新疆分院院长彭加木失踪的谜题。 <br />
此事件已经是20多年前的事情了，为什么会被提起？ 因为一个网友在天涯发：中国有没有调查神秘事件的官方机构？<br />
<br />
双鱼玉佩事件是灵异界必不可少的话题，然而相关资料却少之又少，与消息被封锁的说法相比，双鱼玉佩本身就是一件未被揭开的古老秘事，所以无解的可能性远远大于消息被封锁的可能性。<br />
<br />
　　早在十年动乱之前，大概是1957年到1962年之间，我国大西北地区发生了一些事情（据说当时罗布泊发现了一个古城遗址，一些青年想去淘些古物，后来不知那里发生了什么事情，那些青年死的死，疯的疯。据说那些疯者看起来像是鬼上身，但又不是。那些疯者行为异常活跃，最后全都筋疲力尽而死，验尸后发现他们身上有未知毒素、胃中残留未知植物，就是因为食用了此植物才使那些幸存者发疯。这些疯者脚部已经磨烂，也就是说他们毫无知觉。更令人震惊的是，他们带回来了一些拓片和一些古代装饰品的碎片还有一块玉镰），当时已经开始进行了一些调查，但不够充分。然后文革爆发，国家行政机构实际处于瘫痪状态，此事就搁置了。<br />
<br />
　　文革结束后，军方首先提出继续调查（所以彭加木综合考察队的队员组成其实是很微妙的，主要成员是军队里的一些人，还有很多方面的专家，包括民间的专家，大家可以猜到是什么人。但最大疑点是文革之前那些去淘古物的青年的出事原因，而又在其胃中发现未知植物，军方将此事故断定为未知生化事故，其实是为了培养特种部队），所以最后选定为彭加木领队，罗布泊之行的主要任务就是调查古城遗址、事故源头、采集植物标本。此时只是一个单项调查项目，国家并没有成立专门机构的计划。但是，出人意料的是调查的结果有些匪夷所思，古城遗址找到了，事故竟然再次发生。他们去了哪里？遇到过什么？这是在当时就列为绝密级的东西，去的时候是大队人马，回来的人却没有几个，这其中还有一人受了重伤，彭加木同志突然失踪只是一个个案而已，之所以被重视是因为一起失踪的还有那神秘的植物标本！<br />
<br />
　　有罗布泊的牧人报告发现出现这种情况：天气发生异常，地表环境有短时间的重大变化，随即又会恢复原状。<br />
<br />
　　虽然彭加木的考察队出现了严重事故，但是也有了重大发现――他们找到了一个基本保持完整的工程设施（很难形容这个设施），设施里有大量设备，大部分都失效了（或者到目前为止还不知道如何使用），个别设备的功能被甄别出来了，其中最重要的发现就是――双鱼玉佩。<br />
<br />
　　为什么叫双鱼玉佩？不是因为外形，而是因为研究人员在实验室里初次发现它灵异的功能时，是用一条鱼做实验的时候，玉佩突然启动，一条完全相同的鱼被复制出来！<br />
<br />
　　为什么彭加木失踪了？不是这个人找不到了，而是出现了两个彭加木！在此情况下只能对外宣布彭加木失踪。这只是彭加木消失的一种可能性，关于他消失原因的传闻不计其数，其中可能性最大的就是他晕倒后被风沙掩埋。<br />
<br />
　　1956~1960年之间，新疆出现了大量的镜像人（复制人），部队和百姓都被复制了。但是，后来毛主席把原子弹的靶场选在那里，直接全部解决了。<br />
<br />
　　50到60年代，罗布泊经常出现异常，经常有目击者发现根本无法用地球科学来解释异类生命，随着影响面的越来越大，政府开始介入，最后由于相互之间的缺乏了解，与某些异类起来冲突。那些异类的一些载具和行动方式根本不是地球人类可以理解的，由于冲突中的被辐射源照过的人会变成无生命特征的生命体。所以在那个时代精确的对罗布泊常有异类出没的地区进行了几次核爆（对外则宣称是核弹测试且只引爆了一颗核弹）。<br />
<br />
　　中国核爆问题得到美苏两大国态度大转变式的默许，特别是前苏联的容忍，是因为试爆地不远处的确出现了所谓“闯入者”。这些所谓的闯入者其实就是被地底深处的细菌感染的生物。美国的一个导演躬逢其会，后来还拍摄了类似的电影。<br />
<br />
　　那个时候出来的沙民（国民党残余），中国有电影拍过这段事情。他们生产力极顽强，妇女没有血压了还能自然分娩，夜间借助微光即可精确射击，射杀监视的战士。就算一名老年沙民的体力也接近一名年轻的士兵。这批人后来就没有下文了。<br />
<br />
　　后来80年代彭加木率领考察团进入罗布泊，他们前期工作做得非常周密。但最后还是出事，其原因不在彭，而是考察队中出现敌特，且牵涉到高层博弈，所以最后只能放弃对彭的寻找。但彭一行的目的已经被当时的外媒报道（见亚洲自由之声）渲染了 。<br />
<br />
　　后来又进行了多次寻找彭的秘密行动。因为彭警惕到特务的潜入，而他对考察组的不信任导致要独立求援，找水是一个暗号而已。考察队当时严重缺水，考察队向部队求救要求送水后部队也答应了，但是彭加木却还是坚持一个人外出找水，并且没有和任何队员打招呼，只是在车里面留下一张纸条“我向东去找水井”，就一个人独自走进了茫茫大漠之中，过了几个小时队员看见彭加木还没有回来，怕他出事，赶快开车沿着他的沿途的脚印去寻找，结果一直找到脚印最终消失的地方也没有找到，后来国家曾经动用军队进行了几次大规模寻找也一无所获，彭加木就这样莫名其妙的在大漠中人间蒸发一般消失的无影无踪，直到今天依然下落不明。其随身携带的数据也随之湮灭，不过不知道这究竟是好事还是坏事。其实当时的很多问题，现在还没有搞清楚。只能说人心比灵异事件更复杂，有时候很难分清敌友。<br />
<br />
<br />
<br />
　　传说新疆罗布泊是平行宇宙的交错点。 而彭加木是神秘的双鱼玉佩事件的“关键人”……<br />
<br />
为什么政府要花那么大的代价去寻找一个生物学家？彭加木到底发现了什么不为人知的秘密？因为彭加木孤身带着所有标本，当时彭加木带领的考察队发现的秘密基地甚至可能是外星人的基地，越推论越可怕，而且整个事件隐约指向一个绝对让人战栗的事实……<br />
<br />
1. 罗布泊病毒的真身<br />
<br />
　　按照网上各种关于彭加木的帖子来分析，确实属于一种植物病毒，该病毒并不直接感染人类，之所以出现了人类的感染者是因为有人把带此病毒的植物吃到了肚子里，在出现人类感染者后，该病毒很可能以体液交换的方式来进行传播，所以危险性就变的非常的高。而沙民事件也应该是由于同样的关系所造成，根据沙民事件还可以推论出，有部分人在被此病毒感染后并没有丧失神志，那么此病毒确实可以用来制造超级战士，所以此项目有军方背景而且搜寻彭加木的规模如此之大也可以理解了，这样也解释了为什么一只科考队里为什么有沈冠冕和彭加木两个植物方面的专家了，同样也说明了彭加木的第三次科考就是为寻找感染此病毒的植物。而彭带走的样品应该就是此植物的标本。<br />
2. 感染者<br />
<br />
　　《丧尸生存手册》开始所提到的丧尸围攻电站（气象站）就发生在罗布泊，所以围攻气象站的所谓的“丧尸”必然是感染了罗布泊未知植物病毒的感染者，由此可见罗布泊未知植物病毒类似于《生化危机》的T病毒，不过这个是80年代末的事情，据此我们现在可以断定此事件确实的发生过。而且更可以推论出，感染者并非完全丧失神智，而是至少保留了一部分的人类本能，否则就不可能做出围攻这样有组织的行为，这里发生的事情相信很可能属于一次实验事故。沙民事件里的远古人类也可以解释了，可能在罗布泊的地下有一个远古人类的遗址，那里的人类就是由于此病毒的爆发而突然全体死亡，由于罗布泊的地理特性，此批人类在死亡后尸体并不会完全腐化而是成为干尸，这样客观上就有让他们体内的病毒在低温干燥的情况下进入休眠期而得以保存至今。而同样可以推断，此病毒在感染人体后会感染神经细胞，也就是接管了大脑和脊髓的部分功能，相信这也就是感染者保持部分人类本能的原因。在经过了漫长岁月后，相信在50年代就有人类（很可能是国民党残余军队）闯入了此遗址，并且使用了火把照明，导致遗址内温度升高而让病毒从休眠期苏醒，进而发生了类似诈尸的行为，在人类和远古感染者发生冲突后，有部分人类得以逃脱，也有部分远古感染者追出地表被人看见，这样就成为了沙民事件里的远古人类。<br />
3. 遗址<br />
<br />
　　此遗址的入口应该只是个盗洞，入口的位置应该是位于盐碱地上。这样推论的原因是因为荒芜的罗布泊最多的就是墓地而且人烟稀少，所以沙民跑到罗布泊想生存下来，似乎也只能靠盗墓为生。而盗洞应该是不可能开在沙漠区域的，因为随风而起的风沙足以活埋整只队伍，所以完全没有可能性。这个推论也验证了，为什么最后彭加木的脚印是消失在盐碱地上的原因。找水的人跑到盐碱地上是多么让人奇怪的一件事情，所以真相只有一个，彭加木根本不是去找水，而是去找遗址的入口。而遗址入口的大概位置，沙民在和军队60年代有过接触，也许军队就是从那时得到的。结合上贴的推论，可以断定彭加木找到了此入口并且进入了里面。相信彭加木最后应该已经被找到，但是彭加木很可能已经被感染，所以做为机密的一部分没有让外界知道，但是彭加木的家属或许已经隐隐约约的知道消息。此遗址的内部区域，应该是位于某个地下洞穴之内，很多的小说里都有罗布泊的地下深渊的描述，典型比如天下霸唱（张牧野）的，相信也不是空穴来风。公开的论文里也有罗布泊地下水带的描述，这个就说明了罗布泊下面是有洞穴存在的，只有河流没有洞穴可能么？但是很有意思的网上找不到任何关于罗布泊地下洞穴的公开资料，这个是不是另种形式的欲盖弥彰？<br />
4.双鱼玉佩<br />
<br />
　　今天看来，这个“双鱼玉佩”装置有可能是一个“超人类文明的时间机器或物质转移装置”，极有可能是被运用于某种物质的超距输送的。这种装置使被传送的物质具有了类似于佛教中的神足通的功能，即可以自在无碍的在多个物质空间进行传输。当复制出一条鱼后，科学家们感到很惊奇，为了证明复制的鱼和原始的鱼之间的关系，科学家在鱼的一侧作了标记，结果复制出的鱼也有这个标记，不过位置是相反，非常象中国的阴阳太极鱼的阴抱阳，阳负阴的藕合结构。两条鱼在同一时刻下的动作完全不同，就象是两条不相干的鱼在游动。为了证明鱼之间的关系，科学家把其中一条鱼注射了毒药，这条鱼很快死了，但奇怪的事出现了，另外一条鱼仍然活着！但在七小时后这条鱼也死了，于是证明了这两条鱼之间的关系仍然是同一条鱼，只是经过玉佩装置的功能，呈现了两条处在不同时空状态下的不同状态。从鱼都死亡的时间延续上说，这个装置往返另一个未知物质空间的时差在7小时，天知道那是个什么世界？<br />
<br />
　　由于这个神秘事件的出现，极大的震撼了中国的科学研究方向，因此在八十年代中叶出现了气功热，人体研究热，象严新，柯云路等。不过那都是骗子，是神棍。真正的这些东西是少数几个国家雪藏的顶级秘密，因为据说从其中预示的人类未来命运结局来说，保密比公开好。<br />
<br />
　　实际上，这个双鱼事件引发出一系列的深思，我们人类对自己，对地球，对这个星际知道的太少了，在当今科学探索的前沿地带我们遇到了一个前所未知的领域，尽管我们很想勘破那个神秘的世界，但这个又是当代科学所不能掌握和解释的。这个装置的研究至今没有任何进展，有说这装置已经损坏了，有说暂时封存起来等以后研究。<br />
<br />
　　但是无庸讳言，双鱼事件促使了中国科学界的观念转变，尤其是对辩证唯物论的条件性和局限性的疑问。<br />
