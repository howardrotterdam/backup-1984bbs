<br />
<a href="http://blog.sina.com.cn/s/blog_59392da10100gvon.html" target="_blank">http://blog.sina.com.cn/s/blog_59392da10100gvon.html</a><br />
<br />
对当前经济形势的小思考(2010-02-05 20:52:53)转载<br />
标签：财经&nbsp; &nbsp; &nbsp; &nbsp; 分类：政经思考<br />
我国现状是：以暴力和宣传机器为代表的一亿生活保障相对稳定而丰裕的公务员，管理十二亿多自由职业者。这是国有化真相。并非经济政策，而是财富攫取，以特殊分配给公务员使其安定并追随，从而好放牧另外十二亿多不安定分子。你是否感觉和身边公务员朋友除了房子女人没有共同语言？这很正常，因为你所想表达的都是他们忌讳、害怕听到的。他们每多听一句，都会感到危险和不安。<br />
<br />
这种情况导致：要想社会稳定，必要掌控财政，以相对高价豢养公务员（政权可怜到连五毛党都必须花钱支付劳动），则必要将巨额利润产业收归国有以利分配。则必然使民企和自由经济空间逼仄，则必须靠政府投资拉动消费和需求，雪球越滚越小，空间越走越窄。如果天平平衡临界被打破，则难收拾。<br />
<br />
究竟如何拉拢和豢养公务员？试举我一社科单位公务员朋友为例。月薪5000多，每年只要从图书馆或网络扒拉一些材料写作论文，就可申请五到十万研究经费，而所长级别每年所申请经费则可达几十万层级。过年过节都有礼品发送，每年可有三四次国内外免费公派旅游机会。试想，如此生活和保障，要想让他们发声、独立发声甚或站在对立面，岂非天方夜谭？<br />
<br />
而以清史撰写工程为例，国家拨款数以亿计，清史工程已成大生意。扼控财务者均为仙风道骨的老教授戴逸和其亲友下属（此老早些时候还因为某骗人教材上挂名总顾问为他而出过丑闻）。阎崇年作为部分具体撰稿，报酬就以五十万计。试想，如此情况下，你能指望清史写作独立思考？你能指望这些本应为社会和人民进步提供理论支持的这家那家，能不经常说出发进步反社会乃至反人类声音？<br />
<br />
这仅是在国家权力金字塔上喝汤层次的文化工作者，则可想掌握政治权力和暴力机器权力的公务员，豢养他们要何等代价？<br />
<br />
有些人看我这么说可能不舒服：我们也是公务员，也穷得要命，是弱势群体。对了，你们也是弱势群体，但那是相对而言，相对公务员强势群体而言。但你们相对更多的真正弱势群体而言，你们的生存条件好太多――温水煮青蛙效应来到。你们从不发声，甚至做帮凶（比如无毛、妈妈党），但总有一天你们必然被抛弃。<br />
<br />
为什么大多数公务员也总有一天被抛弃呢？很简单，现在给小恩小惠，你们就不出声，甚或帮凶，社会很稳定，你们作为青蛙在温水上享受时，公务员最上层，他们正以末日的心态和火箭的速度，以明目张胆的手段（因为，你们都噤声不言啊），极端攫取、分配国家甚至是私人财产。<br />
<br />
他们需要借你们维护社会稳定的这段时间差，攫取分配完财富，如果社会真正崩溃时，即是你们被彻底抛弃的时候。那个时候，我死之后，我走之后，洪水滔天。<br />
<br />
问题在于，这种巨额豢养一亿公务员以扼控稳定的做法非常不妙。它的维持完全决定于经济必须持续高速发展。然而常识告诉我们，既毁坏自然环境，且因为舆论管制破坏诸多产业（比如难以计数中小网站从业者失业，传媒和文化产业哀鸿遍野），更兼控稳而攻陷民营经济空间，如此竭泽而渔，求经济持续高速发展安可得？<br />
<br />
高价控稳――国进民退――经济发展，本就是非此即彼的悖论，欲求兼顾安可得？它的不稳定性和危险性，不用吴敬琏周瑞金这样的良知专家，三岁孩童也可明白。<br />
<br />
在这种稳定天平摇晃不定的情况下，无论国内还是国际经济环境变化（根本无须政治环境），都可能推到稳定多米诺骨牌。国内言，高价豢养公务员和国有化的悖论，足以自毁经济长城。一旦公务员不稳定，一旦你和你的公务员朋友都有同仇敌忾共同语言的时候，则可推知公务员顶端之军警和权力掌握者的心理状态。<br />
<br />
军警心理状态如果不稳，并不需要或者说他们自己主动会有什么动作。而是他们在发生极端群体性事件时，可能静观其变，装聋作哑，当然，最坏者是什么情况，三岁孩童都会明白是政变。<br />
<br />
而从国际环境言：近日又见政府和跟随着怒斥奥巴马见藏僧，鼓吹封锁波音公司。可怜的民族主义国人，还没有看到更巨大的阴影和危险。你封锁波音，你考虑过波音和空客联合封锁过我们没有？希拉里演讲不可作常观，商人和政客逐利，所以此前多少次六国诸侯联军杀到函谷关，被秦国合纵连横，各个击破。<br />
<br />
而现在，有底线和尊严的政治家，正像“自由指引战斗”油画一样，振臂唤醒商人和政客良知，我们可以将希拉里演讲，理解成牵头西方商人联合体、政客联合体，穿针引线，促成反秦阵线联盟。所以奥巴马近日作为并不奇怪，或许，未来如果西方政客和商人还有更多行动，我们并不需要奇怪。按咱家《环球时报》最新的标题说法：总统开辟汇率新战场，参院要逼企业学谷歌。兄弟，学谷歌可能，“逼”字谈不上。美国企业不是像你们这样的婊子，参院也不是宣宣这样的老鸨。听这标题，怎么感觉部分人内心在紧张？<br />
<br />
我们并不是鼓励西方国家封锁我们，那样势必给人民带来更多痛苦，但至少，如果他们以后不再合作，如果你封波音，他们波音空客联合在一起反制你，真是正常之事。我们应该清醒看到，中国人口红利已微，向外倾销有违健康产品，此市场吸引度正下降。尤其，金砖另外几国包括墨西哥等市场正日益扩大。<br />
<br />
从前只有中国市场，故忍辱偷生或谋利，但现在，如果把他们逼急，需要他们付出所不能承受的自由或人格等代价，金砖三国和各洲无数新兴市场足够供应他们。他们不是不可能撕破脸皮。<br />
<br />
反过来又说到国内环境：如果国际联合制裁，珠三角长三角等几地区无数民工――他们是中国目前还算稳定的最大贡献者――将情何以堪。他们不可能回到自己早已不适应甚至是无法自足生存的农村，城市流民是他们必然的归宿。<br />
<br />
很长时间我都在思考中国的临界点以后，会是什么样子，也就是下一个中国是什么样子。古代史我钻研较深，但我想没有太大借鉴意义，苏联史呢？我想同样难借鉴。苏联是戈尔巴乔夫主动改革，一如蒋经国、李光耀等亚洲强人，主动变革有个前提是，市场经济还没有启动，权力阶层没有利益牵挂和陷入利益集团泥潭。<br />
<br />
而今中国市场经济早已发动，利益集团早已形成，权力因为通常身后即亡，像朝鲜这样想延续三代，简直匪夷所思，所以放弃相对容易。利益却可以传宗接代，断难放弃。他必将形成恶性循环，像艾滋病毒一样陷于其中不可自拔。因为你的家族要利益，别的家族也要利益，这跟权力逻辑一样，你如果敢放弃，别人也敢放弃。<br />
<br />
对中国来说，尤其难的是，毛泽东和林彪后代的遭遇，必将使权力拥有者胆颤，我如果不护卫攫取利益，我的后代必将和他们一样。因此，只可能是血仇定律的最高境界：共同发财。而等到利益集团全部共同发财，则这个国家的经济形势和政治形势，将是神州糜烂，不可收拾，还谈何良性改革。<br />
<br />
我们基本可以判断：古代模式和苏联模式均较缺少可能。好端端突然斩首式政变呢？也难。由于严苛的军事调兵体制，如非神州鱼烂陆沉，类似于古代的藩镇拥兵不太可能，宫廷内部政变还是上面说的道理，类似于华国锋时代，权力只有一家，供不应求，政变可能。但现在利益至上，利益可以分享，共同发财，宫廷政变，不符合血酬定律。<br />
<br />
我们首先要承认，变数和偶然无处不在，于此意义而言不好预测。我想，我们需要注意下面情况：因为国内国际的经济政治环境变得恶劣甚至对抗，平衡临界点越来越到极限，人们愈无法忍受，群体性事情越来越多。某一个极端群体性事件，权力逼迫暴力机器压制，人们越来越激愤，终于偶然导致暴力机器旁观乃至更甚。<br />
<br />
我们进而需要注意：暴力机器旁观民变乃至更甚，最后的结果必然是军政府或者军权色彩很明显政府。当然，我们可想而知，那种情况下，不可能是极权，相反，和美国合作，寻求美国帮助是必然，变向威权的民主政府。无论该政府如何混乱或威权，它至少意味：有选举，国家有反对派，当然意味着也有反对派媒体。然后维持此种状态，国家慢慢正常。向左走，变成伊朗和非洲国家，不太可能。向右走变成巴西或者俄罗斯此类国家，很有可能，从此走出唐德刚所谓历史三峡（此论本身德刚老有些瞎掰，我并不认同），变得正常。<br />
<br />
从今日起，到最后所预测这个状况，我们应该可以判断：它不可能需要二十年以上。以上诸条围脖，就是我浅薄的当代中国思考。我希望我的国家能妥善处理一切。</div>

							
							
							
							
