																											<div id="postmessage_645743" class="t_msgfont">石扉客推荐<br />
RT @shifeike: @魏寒枫 这篇对经济形势的思考值得一读，除了因围脖体导致行文不够简洁和清晰外，该说的都说到了。最重要的是，看来现在大家对未来局势的预判越来越接近共识了（不过我没他那么乐观）。<a href="http://sinaurl.cn/hCaX4" target="_blank">http://sinaurl.cn/hCaX4</a><br />
