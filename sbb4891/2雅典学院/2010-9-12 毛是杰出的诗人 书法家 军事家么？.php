<br />
太子看见鸡蛋，遂用来蛋炒饭，大白天生火做饭，袅袅炊烟让美军战鹰发现了，于是径直飞来，扔下了固体汽油弹，其他人看见了，迅速逃出。而太子与高参谋认为是炸弹，于是爬到床底下，但是燃烧弹，并不爆炸，等火势扩大以后，太子升天。<br />
<br />
