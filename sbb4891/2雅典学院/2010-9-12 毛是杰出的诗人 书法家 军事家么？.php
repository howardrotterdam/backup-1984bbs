																											<div id="postmessage_826035" class="t_msgfont">毛是杰出的诗人、书法家、军事家么？<br />
<br />
因为俺在《春游宜湘河掠影》一贴的一张照片说明中，有一句“毛一生虽丧尽天良、祸国殃民，但其‘五岭逶迤腾细浪’的这句诗，颇能说明这一带的地貌特色。”于是招来网友的批评：“不管怎么说，毛是杰出的诗人、书法家、军事家……”<br />
<br />
俺就把毛这“杰出的诗人、书法家、军事家”的画皮揭开吧。<br />
<br />
毛是杰出的诗人么？却一直把那首由胡乔木写作的最有诗意《沁园春&#8226;雪》据为己有，贪天之功，不知羞耻。直到胡乔木临死前才说出真相。且他的其他诗作，大都是“词臣”郭沫若代为修改、润色出来的。<br />
<br />
毛是杰出的书法家么？毛在井冈山和延安时，歪歪斜斜的字，写得很臭，很难看。一直受到张闻天等人的调侃。只是在他坐进金銮殿之后，郭沫若才根据毛的狂放不羁性格，建议他学怀素的狂草，才有所进益，才体现出一点特色。<br />
<br />
毛是杰出的军事家么？我只举两例，已足以说明。<br />
<br />
林彪在东北时，开始时，他确实很听毛的话，想一口吞下一只大象，结果被孙立人打得一败涂地，落花流水，差点被赶出国门，流亡苏联。还是在马歇尔的一再施压下，蒋介石不得不严令孙立人收兵，才让林彪守住哈尔滨……后来，林彪终于醒悟，对毛阳奉阴违，自行其是，才扭转了战局――这件事情，在揭批林彪时，还专门挑出来写成批判材料呢。<br />
<br />
在朝鲜战场上，彭德怀也吃够了毛不懂装懂，遥控指手画脚的大亏。后来才学会林彪那一招……彭德怀又招致毛的批判。<br />
<br />
荆楚回贴于摩友之家网上。<br />
<br />
2010-4-27晚上<br />
<br />
