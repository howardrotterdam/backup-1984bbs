													<h2>对党员问题的一点看法（更新）</h2>
						
						
																											<div id="postmessage_630512" class="t_msgfont">　　我是一名中国共产党党员，我不以这个称呼为傲，但也绝不以这个称呼为耻。<br />
<br />
　　看到小组内最近喷党员的帖子挺多，我想说说我的想法，希望能够平和的讨论，而不是进行无谓的人身攻击。<br />
<br />
　　从<a href="http://goo.gl/DNGt" target="_blank">小组调查</a>来看，很多人并不是党员，这个比例到我写这个帖子的时候大概是八成，我想，很多人大概没有参加过任何党的活动，也没有真的想去了解一两个党员。社会现实的黑暗，对当局的不满，种种的怨气都会直接发泄到党员的身上，我想，这是到处喷党员的一个原因吧。<br />
<br />
<strong>一、声明立场</strong><br />
<br />
　　 本帖所指党员，皆指我所认识的，我所接触的，我所了解的，任何推而广之的结论都不一定是正确的。<br />
<br />
<strong>二、为什么入党？</strong><br />
<br />
　　看了一些帖子和回复，很多人最关心的就是这个问题，这个组织这么黑暗、这么暴力、这么让人没有希望，你干嘛要入？<br />
<br />
　　别人为什么我不知道，不过我也知道有很多人入党是抱着功利的心态的。不过从我自己来说，也许是社会阅历尚欠，也许是有些人眼里的“很傻很天真”，我加入这个党，的确是因为我所感受到的党员有着我所欣赏和钦佩的优点和品质。能够加入这样一个组织，和他们相识，相知，让我感到非常的舒服。在这一点上，党并不比其他的社会团体、组织有太多的差别。加入一个论坛，加入一个小组，大多都有这样的一个动机。另外，在这里，我们有一群关心国内外事的人，我们至少可以在闲暇时间里聊聊那些政治、经济、文化等等与我们生活并不切身相关的问题。而这些问题，恰恰是我感兴趣的。<br />
<br />
　　有人也许会不相信，不过要知道，任何人都不能把一个党、一个组织，用一个词汇加以概括。党里面有中坚力量，也有搭便车的人，有开明的，也有固执的。如果说你觉得这个组织完全处于黑暗之中，我想，应该是外人的一种误解。其实，任何一个支部都不大一样，每一个党员也有着自己的价值观。<br />
<br />
　　有人说，你把那个组织描绘的那么好，一点都不客观。可是，这是我加入这个组织的第一印象。无论正确与否，的确很深刻。我碰到的第一批党员，是老师。他们乐于奉献，教学生做人胜过教学生做事。他们是一群好老师，他们是一群好党员。<br />
<br />
　　再到后来，自己辗转了几个支部，也从一个小党员逐渐有了一定的职位，以至后来成立了一个小支部。在这个过程中，我遇到了不少要求加入党组织的人。总的来说，在我碰到的这些人里，信仰马列的，大概有1%~2%的比例。这些人我是非常敬佩的，因为他们相信，所以他们照着那样的信条去做。他们能够为了大利放弃小利，能够服从集体，能够甘心奉献。无论是从党员的角度，还是从人的角度，我都非常敬佩他们。此外，大概有10%~15%的人是抱着功利心和奉献精神二者加入党组织的，而这也是当今最为支部所接受的动机。人没有功利心是不可能的，但是如果你能够为别人带来好处的同时，获得自己的好处，我想这是所有人都可以接受的。这样的人也有，而且还是有一些的，不过相比与其他目的的，要少很多。<br />
<br />
　　先说另一个极端，就是要加入为了粉碎它的。这种目的加入的人我也接触过，比例大概有1%~2%的样子。那么刨去这些比例，大概还有81%~88%的人没有提到。在这些人里，由于没有调查，只是我自己的主观判断，大约有40%~50%的是抱着功利心来的，考研啦，公务员啦，国企啦，之类的。另外还有31%~48%的人，我想应该是随大流进来的，也就是说，入了无害。<br />
<br />
<strong>三、谈谈我对党以及其他的看法</strong><br />
<br />
　　我的想法并不能代表大部分党员，相反，我可能还算是比较另类的一个。<br />
<br />
　　首先说党。中国共产党这个名字，虽然一直没有变，但是大家都知道，其实内部是变了又变。举一个大家都知道的例子，笑蜀的《历史的先声》中，不都是共产党员的言论吗？那你们赞同他们的言论吗？我忘了谁说过这样一句话，唯一不变的就是变化本身。随着时间发展，党是会变的，这是一个大前提。否则，很有可能我也要退出来了。<br />
<br />
　　问题是，党往哪个方向变。我想在小组里的人应该都同意这样一点，那就是现在没往者我们希望的方向变。党的变化，说到底就是党员的变化，因为党不是抽象的概念，而是由一个个党员组成的。有人说，你想通过自己的努力改变一个党吗？笑话。没错，现在看起来这就是一个笑话。但是，正是这样一个个的笑话，才能真的让这样一个庞大的机器发生一些变化，出现一些我们希望的变化。<br />
<br />
　　有人说，我们不需要它变好，我们可以推倒重来。我想问，我们可以吗？假使现在不让共产党作为执政党了，那，谁来？如果现在社会上有这样的一股力量可以完成过渡，我肯定站出来帮忙。问题是，有吗？<br />
<br />
　　无论是不可能还是不愿意，总而言之一句话，到现在为止，好像还没有这样一股力量，能够顺利让我们过上我们想要的生活。<br />
<br />
　　在这样的前提下，一些人叫嚣，谩骂，他们除了把自己的怨气送给了被骂的人，并没有为这个社会做出任何有用的贡献。难道你认为你骂两句，你眼中那个邪恶的组织就会乖乖的听你的话，然后改邪归正了？我不认为有这个可能，除非你拿起武器，起来抗争。<br />
<br />
　　不过，随着经济一体化的不断发展，在中国外有不少人都在担忧着中国的不稳定，而不仅仅是共产党本身。当你拿起武器的时候，当你面向自由女神微笑的时候，你也许会发现，自由女神并不站在你的那一边。<br />
<br />
　　其实我，包括我身边的一些党员，我们正在做的，和所有人一样，希望中国（此处并不特指PRC）好，希望我们的日子能够好过。唯一不同的是我们选择了不同的路径去实现这样一个梦想。因此，在这里，我恳请那些骂人的人，除了发泄你心头的怒火以外，请不要过分指责党员什么。我们的目的是一样的，并没有多大的仇恨。<br />
<br />
　　另外说说问题。现在遇到的很多问题，我认为有个人原因，也有体制原因。现实就是如此，我们无法回避。但是很多问题，并不是因为党员而产生的这样一个结果，也就是说，党员和恶果中间没有必然的因果关系。只要在体制内生存的人，无论是否是党员，都会做出如下的行为，只不过在有的地方，党员是进入体制内的钥匙罢了。一个人拿了钥匙开了别人家的门，在里面杀人放火，但是钥匙是无辜的，况且又不是所有的钥匙都能打开那扇邪恶之门。<br />
<br />
<strong>四、我能做什么</strong><br />
<br />
　　说了那么多，好像我自己什么都做不了。谈起改变，人们总说，你改变不了什么。当然，我一个人肯定无法改变什么。但是，我相信，随着互联网技术的普及和发展，随着越来越多的社会问题被揭露和讨论，随着像1984这样的论坛不断壮大（我想区区一个GFW应该无法阻挡小组的发展吧），肯定有越来越多的人能够在一起讨论，甚至有朝一日能够一起做一些事情。也许几年之后有那么一股社会力量会出现，也许几年之后有那么一些事件会发生，也许几年之后我们能够唏嘘今日的不易，不过我想，我们都在努力着，不是么？<br />
<br />
