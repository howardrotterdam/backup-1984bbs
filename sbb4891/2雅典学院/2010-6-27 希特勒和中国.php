													<h2>希特勒和中国</h2>
						
						
																											<div id="postmessage_706846" class="t_msgfont">希特勒在自传中描述童年在维也纳的生活时，曾经提到过，在自己贫困潦倒时受到过一家旅居奥地利的中国家庭的无私帮助，这个张姓(按原文的德语发音CHEUNG谐译，在英语中也可译为“程”)家庭曾无偿的一次提供给他500奥地利帝国先令作为他的学费和生活费用，还常常邀请他到家里吃饭留宿。当知道希特勒有很好的绘画天分时，这个善良的中国家庭还主动帮他联系了闻名的维也纳大学，希望能资助他进入大学学习。他们的帮助是无私的，是让人无法忘怀的。<br />
　　希特勒曾在谈起自己往事的时候感叹道。后来希特勒掌权后合并了奥地利，但那时无论他用什么方法都再也没有找到过这家善良的恩人们。<br />
　　床头的孙子兵法和资治通鉴---希特勒对中国古兵法和书籍非常喜爱。据他的随行秘书说，经常可以在这位帝国元首的床头看到德文版的中国书籍。希特勒在视察法国占领区和访问意大利时都曾随身携带孙子兵法的简译本，他还经常挑灯夜读并写了大量的阅读笔记。在任命隆美尔为陆军元帅暨北非方面军总司令时，他也曾送给这位名将一本带有自己注释的孙子兵法简译本，让这位忠心的将领感激涕零。<br />
　　鄙视日本看好中国---在轴心国结盟的初期，希特勒并没有预料到日本的发展会如此迅速。当时在美茵茨的军事基地里，他曾私下对自己的秘书长阿姆施太朗说过和这种国家结盟简直是耻辱，他们只会在海边打鱼!在他和一些重要将领的信笺中，他也提出了自己的二分天下设想:以巴卡思坦(当时的波兰东部和苏联的交接处)为界限，西方(欧洲，中东，北非，大西洋西岸)为德意志第三帝国领土，东方(亚洲，小亚西亚，印度，太平洋西岸)为中华民国领土。因此，尽管迫于意大利的压力与日本结盟，但纳粹德国对中国政府的态度一直都相当暧昧不明。甚至在得知南京沦陷后，德国还曾秘密支援了国军一些包括I号坦克在内的德制武器。<br />
　　喜欢中国茶叶---1943年希特勒在波兰疗养时曾收到一个商人进献的极品中国茶叶，从此对中国茶情有独钟，虽然在公共场合为了保持日耳曼优秀传统而饮用咖啡，但私底下大家都知道茶叶才是元首的最爱。因此也一度引起了一股中国风的盛行。英国的特情机构MI 6也曾策划用搀入剧毒的极品中国茶叶对希特勒进行暗杀的行动，但最终被盖世太保察觉并破坏。<br />
　　蒋介石向德国派了大量的公费留学生。至于抗战前的大量德国军事援助那就不用说了，直到抗战开始后还不顾日本的强烈反对，小规模给中国军援，直到1941年。德国盖世太保不太找中国学生麻烦，甚至开战后亦然（看书《留德十年》）。1936年奥运会，德国出资帮助中国代表团，这些也是事实。<br />
　　为什么这样，因为曾经有过中国夫妇救过他。希特勒曾说说：“中国人并不能等同於靼鞑人和匈奴人，他们是特殊的人种，是有一些文明的人种。”并对中国表示友好，曾称赞蒋介石。他对中国，远好于当时要杀光有色人种的3K党盛行的美国和视有色人种非人的英国。<br />
　　希特勒曾经被中国人救过所以在德国，有很多中国人便过得很好，比如说，蒋公次子纬国，便是德军军官。德国对中国，远好於英美。另外，希特勒承担了1936年奥运会，中国代表团的全部费用，并称中国人是“特殊的、不同一般的有色人种，理应享受更多权利。”<br />
　　不得不说，二战后，在犹太人的极端丑化下，希特勒成了一个恶魔。犹太人出于自身利益，有理由痛恨希特勒，中国人有理由吗？&nbsp; &nbsp;<br />
　　任何虚假的东西都经不起时间的考验，犹太人伪造的大量所谓德军暴行，多年后被证明有些是虚构的。现代人应学会更为理性的看待二战，看待希特勒。二战中，希特勒最真实的身份是战败者。其他都是可以任意涂抹的！希特勒发动战争的原因也很简单----反抗压迫。一战后，德国承受了难以承担的不平等条约，这是诱发战争的主要原因。</div>

							
							
							
							
