													<h2>不错的评论唐山大地震的文</h2>
						
						
																											<div id="postmessage_764026" class="t_msgfont">震还是不震（李承鹏）<br />
<br />
<br />
<br />
有人批评冯小刚在撕一个民族的伤疤。不带这么表扬他的，这么多年来国家机器和知识份子一直在帮助电影界捂伤疤，撕伤疤这样的善举至今我还没在公开院线里看到，说冯小刚撕伤疤，是在揭发审查部门把关不力。<br />
<br />
<br />
<br />
以中国电影严厉的审查机制，冯小刚没胆也没能力揭伤疤，揭了，冯小刚迅速就会成为敏感词，敏感词电影势必将转成地下电影。曾经一些青葱年华的导演也想揭伤疤的，撕着撕着却变成创可贴，比如拍出过霸王别姬的陈凯歌，还比如拍过活着的张艺谋，他曾活着，但已经死了，转弯灯都不打就迅速掉头成为世上第二好的团体操导演（第一好在朝鲜），那都不是创可贴而是贴心小棉袄。还有人说看大地震会让灾区人民二次受伤，可在《新闻联播》和《感动中国》里灾难一直是当成英雄事迹来报道，要伤，早二千次受伤，还在乎多一部电影？对于一个拍市民喜剧起家的导演，你要让他成为斯皮尔伯格或者反战独立导演，就矫情了，冯小刚注定成不了思想家、寓言家，这块土地上也永远产生不了思想家寓言家，他拍喜剧是给人民挠个痒痒，拍悲剧是刮个痧，这个俗人早就想好了的，也是这么做的。<br />
<br />
<br />
<br />
大地震在电影上是可以批评的，有些毛病比如剧本就写得有些像电视剧纲要，中间部份都写断掉了。可上纲上线就没意思，他要是像有个导演那些企图把王家岭**拍出一部《八天八夜》来，那才叫发国难财，才是用眼泪绑架观众。这年头谁绑架谁，其实是楼市绑架了股市，股市绑架了菜市，菜市绑架了房事，房事绑架了车市，官员绑架了人民，人民反绑架人民……这是一个在各个领域互相绑架的国度，大家叫它为集体主义和爱国主义，就是这样。<br />
<br />
<br />
<br />
半年前看完阿凡达首映，我写过阿凡达是钉子户的伟大教材，我说中国电影技术上落后好莱坞五十年，人性上落后五千年，现在仍坚持这种判断，不管冯小刚是否拍了李元妮跟方登之间的亲情，因为包括辛德勒名单在内的（虽然它们都是万恶的资产阶级电影）宣布的是弱势向强权的挑战，而我们的无产阶级电影是弱势跟更弱势之间的纠结，前一种挑战其实成就的是信仰，后一份纠结到最后就修炼成磨叽了。没办法，中国几千年艺术史就是磨叽史，不敢直书帝王家事，藏着掖着还夸它是“春秋笔法”，没一幅直面社会现实的名画，画些云山雾绕的还说其实那是“写意”，写着写着发现逻辑狗屁不通断点太多，最后大家定义这就是“留白”……春秋、写意、留白，影响了我们几千年，指导我们永往直前，我们都这样，包括冯小刚。<br />
<br />
<br />
<br />
没一些写手肉麻吹捧的那么好，可我还是想说大地震是可以一看的，因为它比别的更好，这里的人民不敢动，这里的人民可以感动，哪怕完事之后你觉得这其实是瞎鸡毛动，我们就剩下这点暖意可以动一动，这就是这个国家的电影基础，跟我们的文学基础音乐基础是一样的。我觉得冯小刚有个地方是不错的，那就是他更多是拍现在而不是古代，你看最近的导演越拍越古装了，无极是哪个年代的，黄金甲和三枪是哪个年代的，赵氏孤儿是哪个年代的，三国、红楼，最近的也是南京和梅兰芳……大家都在装，而且不准现在装，得古装，这样很安全，可以过审，下一步只得拍元谋人河姆渡起源的电影了。我接触过很多导演很多公司，他们把酒言欢还对此自鸣得意，觉得自己政治上很成熟，把握尺度很老到，这其实是一个国家影视创作力的悲剧。<br />
<br />
<br />
<br />
至少冯小刚还可以偷摸弄点现实主义的，从亲情杀出个偏方，从很多知识份子觉得这当然不带劲，可你想让冯小刚怎么着，历史原因，让他直接拍汶川大地震死亡学生或者王家岭**死亡者名单吗，那他，一会儿就先于A未未之前就挂了，他的名单都不见诸于报端，还弄个鸟的辛德勒名单，中国是不准拍名单的，因为那份名单就活脱脱是一份民族命运的账单，谁都不敢轻易在自己的任期内开禁，埋单。中国很多的事，留给时间解决。<br />
<br />
<br />
<br />
汶川的事太近，不能说得太细，1976年的事情我还记得一件：那年我还很小，有天成都打金街上来了一个黑墩墩的小孩，十四五岁左右，他是专门打蜂窝煤的，就是把街坊家的煤渣子用一种铁模子打出成型的蜂窝煤，在那年代这是居家节约的好办法，很吃香。这小孩煤打得很结实，但不收钱，饭管饱就行，饭量奇大，一顿可吃五碗。我们不知其名就叫他五碗，街上常响起“五碗，打几个蜂窝煤”，就见他拎着煤模子跑去了，他吃饭的速度超过打煤的速度，有些像直接把一碗碗饭搁到胃里。后来才知道他其实是一个孤儿，前年全家跟着姐姐嫁去唐山，因为唐山当时的工资较高，后来碰上地震全死光光了，就剩他跑回广元老家，可老家遇大饥荒，都吃一种白色的泥巴叫观音土，五碗说，那土吃了就是肚子胀，想放屁又放不出来，乡里很多人就拉不出屎来死了。<br />
<br />
<br />
<br />
五碗说起这事从来都笑眯眯的，看不出一点悲伤，他继续打煤，继续快速吃饭，他对生活很满意，对新住所的避风程度也很满意（那是街边一根空置的下水道管子）。他大概在打金街工作了两个月，后来就被抓走了，因为有天凌晨运送战备物资的军车经过这条街，掉了一些罐头，他从睡梦中惊来，很高兴地捡，被一枪托砸翻，带走。<br />
<br />
<br />
<br />
本来他也没事，可后来遇上“严打”（这称呼当年就有），为了凑人数，就以抢劫军车名目把他算上，就崩了。这也是一个地震的孤儿，他的生命中最重要的就是吃饭，为了一个罐头，就这么交待了。当时执行枪决是要先在解放牌卡车上游街的，我见过他混在其他一些犯人中间站在卡车车厢上，鼻涕拖得好长，晶莹且不断线，还在笑，那笑容绝对真诚和幸福。可能是执行前吃了顿罐头饭。<br />
<br />
<br />
<br />
这些是不能拍成电影的。因为这是真正的悲剧。<br />
<br />
<br />
<br />
关于悲剧的悲剧是，即使这样拍出来，最不满意的不是有关部门，而是人民，因为这太像他们的生活了，他们是来看电影的而不是来看自己的，你理解吗，人民是花了币的。所以要拍的话，只能是五碗长大了，还办了属于自己的蜂窝煤加工厂，有一天在出门讨欠款时，发现客户的妻子正是自己的姐姐，而客户可能还是当初开军车的司机，天哪，亲亲的姐姐其实是没死的，她活得还好，只是多了一些皱纹，姐弟倾述着离别三十年来的衷肠……<br />
<br />
