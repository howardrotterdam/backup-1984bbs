																											<div id="postmessage_713176" class="t_msgfont">陈志武：很多普通人支持房产税是被卖了还帮着数钱<br />
2010年06月29日 15:04凤凰网财经<br />
陈志武 美国耶鲁大学管理学院金融学终身教授<br />
<br />
“只要一推出房产保有税，就可抑制房价”，那不如就发房票算了！回到计划经济时代，变回单位分房。我在美国3个州住过几年，也买过房，没有哪个州会因房价高而增收的。利率为什么不提高呢？美国遇到房价高涨时，联邦政府不会做太多，只会通过制约融资来减少大家买房，而不会通过增加税收。 <br />
<br />
征收“房地产税”国家的土地都是永久产权。在借鉴国外经验时，不能忽视制度上的差异。美国各级政府都不会经营土地营利，但中国有的地方政府70%-80%的收入都来自土地财政。如果忘了这个前提，那就是不科学的。<br />
<br />
任何转移老百姓财产的费或者政策，都应经过严格立法程序。物权法对此也有规定。人大应把这事接管过来。在任何一个国家，私人财产保护最后的底线都是它会不会被任何合法或者不合法手段往政府手里转移。不能因为房地产或宏观调控需要，而轻易攻破底线。在我看来最危险可怕的是，把房产税的决定权留给地方政府，各地政府都向中央提交方案，为什么那么积极？明摆着给它进钱的机会。地方政府增加新的税种的权力，会使之后给整个社会和私人财产带来的伤害达到新的高度。拆迁即是例证。 <br />
<br />
房地产价格短期下跌，银行坏账会增多，对经济的影响远远超出银行业。增长模式的转型也就成了空口号。4月地产新政前，很多人已买房，跌30%就等于首付全赔光，而银行的70%还在，资不抵债。我不是支持房价上升，但硬着陆带来的伤害很大，尤其是用政府的手强拉下来，还不如通过市场化手段慢慢调下来。 <br />
<br />
很多普通人支持房产税，那是被卖了还帮着数钱。这种税对普通人特别是年轻人的影响较大。表面是买房子的人才要承受，实际上会转嫁到租房子人身上。最可怕的是房地产市场搅动着民意，给地方市场不受制约转移老百姓财产的机会。世界上所有政府对税有一个共同的特点：只上不下。房地产税一旦推出来，以后要拿掉比现在要难，后代只会一直背下去。受冲击最大的是中产阶级。 <br />
<br />
“住房保有税”跟全面建立小康社会的目标是背道而驰的。1998年的住房全面市场化的改革，给中国带来的贡献是根本性的。普通老百姓家庭终于有房产了。而这个税那个税，会侵蚀掉房产作为中国家庭财富的意义和价值。</div>

							
							
							
							
