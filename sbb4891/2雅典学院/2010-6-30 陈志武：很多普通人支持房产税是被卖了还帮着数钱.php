　　对眼下正在展开的楼市新政而言，房产税到底是天使还是魔鬼？很多人恐怕都会有不同的看法，就像上海方案中的70平米规定，对于绝大多数人来说，这个平均面积的标准都是合适的，但是，对于那些第一次购房的单身者来说，这就意味着他只能购买70平米以下的住房，要是购买一个两居可能就要交税，这样的规定是不是也有他的不合理之处呢？答案也许并不在房产税自身，而在于整个房地产市场乃至整个宏观经济的结构性调整。如果这一轮调整能够真正扭转财政增收依赖土地，经济增长依赖房地产的跛脚格局，房产税适时出台无疑会起到政策效应放大器的作用，促进发展方式的转变。但如果不去反思改变导致高房价的根本原因，只是看到征税的短期效应，很可能就像大家担心的，房产税的成本会像击鼓传花一样，最终转嫁给购房者。不管怎么说，对税收这项权利，政府都应当严肃而审慎地使用，确保它的稳定和长期性，不能仅仅看作一个抑制房价的工具，一针救急用的激素。</div>

							
							
							
							
