													<h2>“崛起”应源自文明龙应台北大演讲遭封杀</h2>
						
						
																											<div id="postmessage_770137" class="t_msgfont">“崛起”应源自文明龙应台北大演讲遭封杀<br />
<br />
【DJY8月3日讯】（DJY综合报导）台湾作家龙应台8月1日在北京大学百年纪念讲堂发表演讲时表示，她拥有一个“中国梦”，希望中国的“大国崛起”根源于文明的崛起。这是她首次获准在大陆公开演讲，但演讲内容遭北京封杀。<br />
<br />
龙应台8月1日在北京大学演讲，以犀利之词讥讽中共的“大国崛起”不应只是“军事耀武扬威，经济财大气粗，政治唯我独尊”，应该根源于文明，不然这种崛起“最终将带给人民灾难”。她还呼吁对人权和言论自由要“寸土必争”。<br />
<br />
龙应台的《大江大海：一九四九》迄今无法获准登陆大陆，但她8月1日在北大百周年纪念讲堂发表的演说，是龙应台首度获准在大陆公开发表演说。<br />
<br />
龙应台的“中国梦”<br />
<br />
龙应台是应广州《南方周末》之邀往北大接受“中国梦践行者”致敬杯颁奖并发表演讲的。她一开始就以反诘之词说：“这真的是北大吗？有1,000颗导弹对准我（指台湾），我还要来谈中国梦？”随即引起台下一阵哄笑。<br />
<br />
龙应台指，看待一个国家是否文明，要看它如何对待少数民族、异见人士与弱势族群，看怎么包容不同意见；“当然，也包括（中国大陆）13亿人，怎么对待（台湾）2,300万人”。她说：“权力侵蚀无所不在，所以个人权利、言论自由一定要寸土必争，绝不退让。”<br />
<br />
她强调，自己对中国是有梦的，这个梦是希望中国的“大国崛起”，是根源于文明的崛起，而不是来自于军事力量或经济富强，否则，这样的大国很危险。<br />
<br />
龙应台说：“谁在乎大国崛起？如果所谓‘大国崛起’，是人民引以自豪的军事耀武扬威，是经济的财大气粗，是政治势力的唯我独尊，那宁愿它不崛起。这种崛起，最终带给人民的是灾难。”<br />
<br />
让下一代有免于恐惧自由<br />
<br />
龙应台称，台湾原有的“中国梦”在经历退出联合国、台美断交后，台湾人普遍认清事实与现实，并逐渐转型为“台湾梦”。<br />
<br />
她强调，“台湾梦”虽有分歧，但不论蓝绿都有共识，也有基本的价值观，“因为权力侵蚀是无所不在的，所以个人权利、言论自由，是要每个人寸土必争，绝不退让。”<br />
<br />
她说：“这是台湾人的梦，是可以与中国人共同拥有的梦，也就是两岸人民基本的价值观。如果两岸在文明价值观没有共识，血浓于水又算什么呢？”<br />
<br />
她并引用1980年代《野火集》风声鹤唳出版后，离台赴欧前留下的一句话：“今天晚上站在这里说话，深怕带来什么后果，但我有梦，希望有一天，我们可以在任何地方讲我们想说的话，我们的下一代有免于恐惧的自由。”<br />
<br />
据悉，当日龙应台的演讲到场者众，北大百周年纪念讲堂座无虚席，其间全场掌声不断。但内地媒体2日对此全无报导，甚至北大网站也找不到有关消息。<br />
