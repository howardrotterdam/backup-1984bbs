台湾作家龙应台应邀在北京大学纪念讲堂发表演讲，她说她真诚地拥有一个“中国梦”，这个梦希望中国的崛起不是来自于军事力量或经济富强，因为这样的大国“很危险”；她希望，中国的“大国崛起”是根源于文明的崛起。龙应台指出，她看一座城市的文明尺度是，这座城市怎么对待精神病患、对待民工、盲流；衡量一个国家文明的程度也有很清晰的尺度，就是国家怎么对待弱势群体、怎么包容不同意见、怎么对待“异议人士”，也包括多数怎么对待少数，“当然也包括13亿人怎么对待2,300万人。”最后她引用80年代《野火集》出版后，离开台湾去欧洲前留下的一句话：“今天晚上站在这里说话，深怕带来什么后果，但我有梦，希望有一天，我们可以在任何地方讲我们想说的话，我们的下一代有免于恐惧的自由。”<br />
<br />
龙应台的这次演讲，赢得了台下听众如雷的掌声。远在万里之外的我，也不禁为龙应台高声叫好。<br />
<br />
读龙应台演讲，使我想起30年前我在北大办公楼礼堂的竞选演说，题目是言论自由。基本内容是依据我在民主墙时期发表的文章《论言论自由》。在结尾处，我讲到：我们的理想是：通过我们顽强不懈的努力，让言论自由的原则真正深入人心，在中国扎下根来，我们的子孙后代，应该生活在这样的土地上，他们能自由地想、自由地说、自由地写作。那时，他们可能会奇怪，怎么还会有这种时代，单单是说了几句话就招来杀身之祸？<br />
<br />
恐惧社会和自由社会是根本对立的。什么是自由社会？有言论自由的社会就是自由社会。我赞成夏兰斯基的说法：“如果人们拥有表达他们自己观点的权利，并免于被逮捕、监禁或者身体伤害的恐惧，那就是一个自由的社会。”诚然，有言论自由不等于就有一切。但正如夏兰斯基所说，o要持有异议成为可能，我们在为自己的理想奋斗时就将会是安全的。那么，怎样确定一个社会有没有言论自由呢？夏兰斯基提出“城镇广场测试”：如果一个人可以走到城镇广场中央表达他或她的观点而免于被逮捕、监禁或身体伤害的恐惧，那么这就是一个自由社会。否则就是一个恐惧社会。这个测试标准很妙：简单、明晰，很有说服力，很容易操作或者说很容易想像。<br />
<br />
30年过去了，言论自由在中国还没有真正实现。但是我们并不灰心，并不沮丧。因为我们看到，30年来，要求言论自由的呼声一刻也没有停息过。包括今天，它又在北大讲堂回响。<br />
<br />
在这里，我愿意再次重申坚持言论自由原则的伟大意义。首先，言论自由的原则是如此正确，如此正义，即便是那些专制统治者也不敢公开拒绝公开反对，这就使得他们在关于言论自由的问题上不得不处于劣势，处于守势，而且必然会节节败退。其次，言论自由原则又是如此简单，如此清晰，稍有常识的人都可以正确领会和把握。另外，言论自由原则对最大多数人具有最大的说服力，它不像有些更激烈的主张，可能会引起一些人不无道理的疑虑，特别是关于社会稳定的担忧。没有人相信有了言论自由就会天下大乱。换言之，在言论自由的问题上，我们最有可能形成最广泛的共识。再有，明确地公开地坚持言论自由原则，并不需要过人的勇气，这就能让更多的人，包括那些素来谨慎小心的人也参加到抵制强权，争取民主的正义斗争中来，从而形成一种强大的而又温和的压力。<br />
<br />
正象龙应台指出的那样，如果中国的崛起，仅仅是来自于军事力量和经济富强，那是很危险的。我们需要的大国崛起，必须是根源于文明的崛起。而文明的尺度就表现在正义，包容，免于恐惧。今天的中国正面临着重大的选择，何去何从，取决于我们的努力。<br />
