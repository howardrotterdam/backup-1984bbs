													<h2>龙应台：希望我们的下一代有免于恐惧的自由</h2>
						
						
																											<div id="postmessage_770136" class="t_msgfont">龙应台：希望我们的下一代有免于恐惧的自由<br />
<br />
2010年08月03日10:31旺报<br />
<br />
台湾作家龙应台1日破天荒在大陆北京大学纪念讲堂发表演讲，她说，真诚地拥有一个「中国梦」，这个梦希望中国的崛起不是来自于军事力量或经济富强，因为这样的大国「很危险」；她希望，中国的「大国崛起」是根源于文明的崛起。图为龙应台昨天首度站上北京大学讲台，剖析两岸人民的认同与差异。（记者宋丁仪摄）<br />
<br />
获选「中国梦践行者」的台湾作家龙应台昨日在北京大学发表演讲，阐述她的「中国梦」，希望中国的「大国崛起」是根源于文明。她更直言，衡量国家文明尺度当然包括「13亿人怎么对待2300万人」。大陆知名作家易中天也分享他的「中国梦」。他直言，如果没有人格独立、意志自由的个人，国家只是个人组合起来的机器，「要他干嘛？」<br />
<br />
文明是…13亿人怎么对待2300万人龙应台北大开讲犀利探讨国家文明尺度赢得如雷掌声<br />
<br />
台湾作家龙应台1日破天荒在大陆北京大学纪念讲堂发表演讲，她说，真诚地拥有一个「中国梦」，这个梦希望中国的崛起不是来自于军事力量或经济富强，因为这样的大国「很危险」；她希望，中国的「大国崛起」是根源于文明的崛起。<br />
<br />
龙应台更直言，衡量一个国家的文明尺度应包括多数怎么对待少数，这中间当然亦包括「13亿人怎么对待2300万人」，此话一出，现场一片掌声。<br />
<br />
美丽岛大审登上北大<br />
<br />
演讲一开场，龙应台以1949年后台湾的音乐演变，来解说台湾历史与民情的转变。她说，她小时候都是听着《反攻大陆》长大，在国民党教育下读着国民党的「教材」；到了1978台湾人的梦开始破碎了，1979年台美断交，台湾被孤立成为国际孤儿。这过程里，有些人觉得被抛弃，有些人的中国梦因情感认同反而被强化了，所以当时出现一首歌《龙的传人》。<br />
<br />
不过她话锋一转，说这首歌的作者侯德健最后也「投匪了」，可能会令人有点尴尬，引起现场一片笑声。<br />
<br />
龙应台还播放胡德夫所唱、李双泽所写的民歌《美丽岛》，她说，这首歌集结了台湾人民的情感，也是部分台湾人开始从移民者真正融入台湾，对台湾浓厚情感的体现。从余光中为《龙的传人》作词到描写屏东枋寮的台语歌，代表了在台湾人民「梦的转化」。<br />
<br />
接着，她将台湾的「美丽岛大审」历史照片播映在北大百年讲堂屏幕上，引起现场一片的骚动。龙应台带领现场观众一个个辨认照片中的人物，在法庭上咧嘴而笑的施明德、如今是高雄市长的陈菊，甚至为「政治犯」辩护的律师陈水扁。龙应台说，照片中与现今变化说明了、台湾民主变迁的过程。<br />
<br />
礼义廉耻台湾价值观<br />
<br />
她说，如今台湾民主乱象，包括蓝绿纷争、本省与外省，还有立法院的打架，在她看来，这都只是表面，「大家不要被表面给骗了」。不管台湾民主怎么乱，在她看来，台湾人都有着相同的价值观，这价值观就是从小被教导的「礼义廉耻」。<br />
<br />
龙应台说，「礼义廉耻国之四维」、「士不可以不弘毅，任重而道远」构成了台湾社会价值观的基座，而且永远不会改变。包括当初红衫军包围总统府时，很多民众升起的天灯上就是写着「礼义廉耻」四个字。<br />
<br />
她强调，这是台湾人的梦，是可以与中国人共同拥有的梦，也就是两岸人民基本的价值观。<br />
<br />
免于恐惧的自由<br />
<br />
龙应台指出，她看一座城市的文明尺度是，这座城市怎么对待精神病患、对待民工、盲流；衡量一个国家文明的程度也有很清晰的尺度，就是国家怎么对待弱势、怎么包容不同意见、怎么对待「异议人士」，也包括多数怎么对待少数，「当然也包括13亿人怎么对待2300万人」。此话一出，现场掌声如雷。<br />
<br />
最后她引用80年代《野火集》风声鹤唳出版后，离台赴欧前留下的一句话：「今天晚上站在这里说话，深怕带来什么后果，但我有梦，希望有一天，我们可以在任何地方讲我们想说的话，我们的下一代有免于恐惧的自由」。<br />
<br />
龙应台、易中天精湛对话响彻北大<br />
<br />
两岸知名作家龙应台、易中天1日在北大百年讲堂的对话精湛，字字珠玑，现场观众多次鼓掌叫好，这场两岸知名作家演讲对谈，由代表「南方视角」的《南方周末》主办、代表「上海帮」东方卫视访问播出，却在「北京」政治中心最具象征性的最高学府北大举行，备受两岸知识学界瞩目。<br />
<br />
龙应台一上台便语出犀利，她说，初获邀请时还想「你有1000个飞弹对准我们，还想邀我去吗？」<br />
<br />
她更在提问时间要求踊跃举手的观众把握时间，因为听说北大可能会对逾时现场「断水断电」。<br />
<br />
中间还询问前一个演讲者易中天：「你今天（讲这些）恐不恐惧啊？」<br />
<br />
易中天说，1966至1976年曾经恐惧过（意指文革）。30年前他万分不能想象今天能站在北大里面，与龙应台做这样的对话。他由衷地感谢北大，谢谢《南方周末》给的这次机会。<br />
<br />
龙应台说，很难得也是首次来到北大演讲，只是这些言论「不知道北大校长在现场吗？」观众纷纷环顾四周。易中天接过话：「管它校长在不在，学校不是校长的，是教员与学生的」。此话一落，现场爆出叫好声与热烈掌声。<br />
<br />
易中天演讲一开头，便用龙应台遭禁的《大江大海1949》做为开场白，尊称「龙先生」，并以「1949龙应台大江大海，20101易中天小打小闹」自我解嘲亦表达对龙应台的敬重。<br />
<br />
易中天：公民有选择的自由直言：没有人格独立、意志自由的个人国家只是机器<br />
<br />
大陆知名作家易中天1日在北大百年纪念讲堂分享他的「中国梦」：「社会进步、国家富强、个人幸福」。他说，20世纪下半最大的解放就是改革开放，赋予公民选择的自由。他直言，如果没有人格独立、意志自由的个人，国家只是个人组合起来的机器：「要他干嘛？！」<br />
<br />
毛思想核心来自斗争<br />
<br />
一向纵古论今的易中天，难得从古代「跳脱」出来论现代「中国梦」。易中天表示，中国曾做过不同的「大同」、「小康」、「治世」的梦，但最后都不免破灭，包括清末的甲午战争、巴黎和会等都让传统的中国强国梦彻底破碎。这也让部分人转入「侠客梦」，所以金庸小说风行不是没有道理的。<br />
<br />
他说1949年之后中国梦有了新版本「人民公社」，这里面也有相信大同理念的成分，但他解析，毛泽东的思想中是结合了马克思的社会主义与秦始皇的法家思想，核心都来自于「斗争」，但结果众所周知，带来的是民生经济的崩毁。<br />
<br />
易中天回忆，曾经听过一个故事，在70年代某个地方非常流行喇叭裤，不过当时喇叭裤是不务正业的服装表征，于是某个校园领导为了「正风」竖立一条标语「喇叭裤能吹响四个现代化的号角吗？」<br />
<br />
有学生悄悄在布条下回了：「请问哪条裤子可以？」极尽嘲讽之能事。<br />
<br />
他说，后来他才知道，这个故事来自于广东。没有这样的南方视角，就没有现在的南方报业集团，也没有现在的《南方周末》。易中天话讲至此，赢得全场掌声。<br />
<br />
中国梦就是个人做梦<br />
<br />
他进一步阐明，只有让公民人格独立、自由意志，国家的组合才不至于是一个机器，否则「要他干嘛？」<br />
<br />
他说，如今中国梦就是「抛弃做集体中国梦」，个人可以做梦了，可以有选择的自由、做梦的自由，可以选择穿什么衣服、可以自由择业，甚至也可以「同床异梦」。<br />
<br />
易中天最后说，只有达到「政治文明、道德高尚」才能走向强国，而他理解的强国，不应仅只追求船坚炮利而已。<br />
<br />
有梦龙应台想找个情人去旅行<br />
<br />
龙应台、易中天的演讲与对谈，北大学子的提问五花八门。理性与感性兼具的龙应台，在一位女学生提问时，不禁泄露出「个人梦」，她说：「想找一个情人陪我去旅行」。<br />
<br />
目前从事写作、教学的龙应台，昨天首次接受北京大学学生提问。现场座无虚席，她说希望可以有女生提问，结果获得这个机会的女学生问：「个人有什么梦呢？」<br />
<br />
龙应台微笑响应说：「首先我希望找一位情人陪我去旅行；然后是希望安德烈、菲力普，我的两位小孩不要嫌我烦，可以让我跟着他们，他们都嫌我了。」语气中显露出母亲的慈爱及难得流露出「小女人」娇嗔姿态。<br />
<br />
北大学生也问「中国梦与台湾梦有没有交集？」，龙应台说可以回归到她认为的共同价值观基座上；还有粉丝提问时先表达仰慕之意，全家都是龙应台的书迷，这次全家动员来北京听她演讲。<br />
<br />
龙应台说，目前她在大陆点阅率最高的一篇文章是〈不相信〉，她鼓励在座的人说，年轻人应该对很多事情都抱持怀疑。她说，这次来大陆，抵达后又要增加演讲数据，网络一搜寻却发现很多网页都是「此网页已……（不存在）」，她真想将那一幕拍下来。于是很多资料只好麻烦助理从台湾找出来寄给她。<br />
<br />
敢言龙应台、韩寒颇有相似处<br />
<br />
比较近日参加香港书展时大陆新锐作家韩寒的一句：「禁书让社会进步」，与新书遭禁的龙应台曾说「一个健康的时代，应该有各种各样的韩寒」，两岸「敢言」作家，血液中似乎有着相同的理念因子。<br />
<br />
今年5月底龙应台携其纪录片《目送1949》赴广州，在中山大学举办大陆首映暨交流会，被问及如何比较80年代的龙应台和当今的韩寒，她曾回答：「一个健康的时代，应该有各种各样的韩寒。」<br />
<br />
《目送1949》是根据《大江大海1949》而拍摄的纪录片，讲述迁台60年的故事。龙应台参考了上百本史料书籍，行迹遍布台湾、马祖等地所拍摄而成。首映会当晚座无虚席。<br />
<br />
龙应台说：「在一个健康的社会，应该有各种各样的、各个年龄段的韩寒。我写作《野火集》时能一夜爆红，是因为当时的台湾有各种各样的禁忌，一拍就会红肿，一拍就会有血迹。」她希望，社会进入开放健康的时代，就应该让每个人的声音都能够发出来。<br />
<br />
去年龙应台新书《大江大海1949》，内容因涉及国共内战，在大陆遭到封杀。龙应台曾在北京谈起这本书时称它为「和平之书」，她曾形容，大陆决策者如果真的看了这本书，应该会说「赶快出吧！」而龙应台的前一本描述亲情的书《目送》则在大陆获得出版，并曾举行新书会。<br />
<br />
作者：龙应台、易中天、宋丁仪</div>

							
							
							
							
