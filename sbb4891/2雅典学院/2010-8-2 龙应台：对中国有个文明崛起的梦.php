主办方的致敬理由：以“吴市场”著称的吴敬琏，对市场经济抱有毕生的信仰。但市场体制在中国落地之后，竟然遭遇一系列困境，也使他大梦初醒，“吴市场”从此升级为“吴法治”。<br />
<br />
易中天：我个人要向你致敬，因为我的女儿是您的学生，您的老师顾准先生又是我的精神导师。我知道顾准临终前您曾经陪在他的身边，在当时那个年代，顾准先生有什么样的中国梦？<br />
<br />
吴敬琏：在革命年代，顾准的心态是昂扬向上的，他的中国梦也是当时很多年轻人和中国共产党人共同的中国梦，就是共产主义的理想。具体而言就是要实现革命的第一步，就是建立一个新民主主义的国家。包括新政协的召开，等等一系列，都是向着建设一个自由、民主、富强、独立的新中国的。但是建国以后，慢慢地就发现事情的进展和建立一个自由、民主、富强的“地上天国”是越来越远，因此也就陷入了苦闷。最主要的困惑是一直在做一个梦，但梦醒以后，不知如何是好。<br />
<br />
