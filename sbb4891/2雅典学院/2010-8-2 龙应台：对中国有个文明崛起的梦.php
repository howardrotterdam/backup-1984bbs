【XTR2010年8月3日讯】（XTR综合报导）以犀利批判笔锋著称的台湾女作家龙应台，1号首度在北京大学发表公开演讲。在演讲中，她剖析了两岸人民的认同与差异，她说她的「中国梦」是希望中国的崛起不是来自于军事力量或经济富强，因为这样的大国「很危险」。她更直言，衡量国家文明尺度当然包括「13亿人怎么对待2300万人」。龙应台的演讲遭到中共当局的封杀。<br />
