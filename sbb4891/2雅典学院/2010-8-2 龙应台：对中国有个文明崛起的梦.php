																											<div id="postmessage_770505" class="t_msgfont">南周“中国梦”活动记录之三：中国三梦<br />
可能吧<br />
作者：lonelyjames<br />
<br />
本文继续介绍我参加的《南方周末》“2010中国梦践行者致敬盛典”。继上篇讲述颁奖仪式的全程之后，今天写的是8月1日易中天和龙应台在北大百年讲堂做的主题演讲《激荡中国：梦的解析》。主要介绍前半场易中天关于“中国梦”和个性解放，个人自由之间关系的演讲。<br />
<br />
更新：感谢可能吧读者梦中僵尸等人上传了演讲录音――不过这是不被许可的行为，因此录音随时可能被删除。有需要的读者请到这个页面下载录音。<br />
<br />
一、概述<br />
<br />
我就预料到8月1日易中天和龙应台的演讲会有很多热心而且目标明确的观众，事实证明果然如此。描写这次演讲的博客文章明显比描写31号仪式的要多，而且《旺报》也派出记者做了比较详细而不是走马观花的报道。此外，不管场内场外，对龙应台感兴趣的人远远多于对易中天感兴趣的人，搞得易中天和龙应台站在一起的时候，总是眼巴巴的看着大家找她签名，还有男粉丝给她献花，自己却什么也没有――演讲过程中甚至连水都没有。您辛苦了。<br />
<br />
尽管现场不准录音，我还是做了现场速记，以下我会把根据速记整理的易中天讲话的全稿呈现给大家。因为是根据速记还原的，所以肯定不能做到语词一一对应，但应该能做到反映现场的原貌。<br />
<br />
我认为，这也是一场不应该被忽略的演讲。尽管易中天明显受到了冷落，但我可以保证这次演讲不同于他以往在《百家讲坛》上的说书，应该可以让一部分人稍微改变一点心目中对他的印象。他的讲话很多次被台下观众热烈的掌声打断，而且说到痛快的地方，还有人高声叫好，整个气氛十分热烈。<br />
<br />
龙应台演讲的细节和现场图片，有很多博客（都是龙应台的粉丝吧？或者至少对易中天不感冒= =）都有写，也有全程幻灯片，所以就不在这里多说她的演讲了。文章末尾附带的几篇文章我都看过，都读过了应该可以还原她讲话的原貌。所以，请不要忘记看完本文后点击相关链接继续观看。<br />
<br />
很抱歉到本文的高潮部分我本人也在鼓掌，所以没有记全，不能把接近原话的记录写出来，只能写出段落大意。希望这不会影响读者们的解读。<br />
<br />
文中颜色、粗体标记、分段和节标题均为编者所加。其中：<br />
<br />
红色粗体 表示能够成为主要论点的语句。<br />
<br />
粗体 表示值得读者关注的语句。<br />
<br />
（大意：）括号内的文字并非听写的速记内容，只是概括的说出那句话的大致意思。<br />
<br />
二、依照速记还原的演讲全文<br />
<br />
龙应台先生昨天在这里接受了&quot;中国梦&quot;的致敬。我现在要以我个人的名义，代表我自己向龙先生致敬。我致敬的方式是一幅对联。<br />
<br />
上联：1949龙应台大江大海<br />
<br />
下联：2010易中天小打小闹<br />
<br />
（笑声，掌声）<br />
<br />
我今天小打小闹的题目是《中国梦：梦与梦魔》（我和周围记笔记的人谁都没听出来是什么字，后来根据其他博客的整理补上的）。<br />
<br />
1、天下梦<br />
<br />
我先给大家讲一个故事。1793年，英王乔治三世派出马戛尔尼勋爵率团来华。表面的目的是为乾隆皇帝补寿，实际上真正的目的有两个：一是通商，二是传教。但是他们遇到了一个麻烦――皇帝坚持他们要行三跪九叩之礼。马戛尔尼说：行礼没有问题，但是根据对等原则，中方必须派和他们特使同等官阶的大臣，向英王乔治三世的画像行三跪九叩之礼。乾隆指示下属“详加开导”：中国是天朝大国，天子雍容大度，朕体恤洋人有打绑腿的习惯，恩准行跪拜礼时解开绑带，拜完了再穿上。这次出访的结果是不欢而散。那么为什么呢？为什么要坚持行三跪九叩大礼呢？原因是中国是“天朝”，天朝的天子是“万民之主”，“普天之下，莫非王土，率土之滨，莫非王臣”，尔国国王来此亦要躬行此礼，何况你一大臣乎？<br />
<br />
从这里就看出当时的中国人，他们的中国观和世界观是统一的，只有一个天下，也只有一个天子。这种中国观和世界观被鸦片战争无情的打碎了。鸦片战争以后，《南京条约》里面一条规定，以后两国公文来往应称为“照会”。这不是外交惯例么？那以前叫什么呢？以前叫“上谕”。我就在想，他们当时是不是还要派一个太监，过来给外国使节们“下旨”：“奉天承运皇帝诏曰：尔等不远万里而来，十分辛劳，赐烤鸭一只，钦此――”。（笑声）<br />
<br />
我们中国人是通过不平等条约学会了与别人平等交往，但接下来就是丧权辱国了。有两件事给了当时的中国人强烈的刺激，一是甲午中日战争，我们居然被“倭寇”给打败了；二是巴黎和会。甲午中日战争虽然屈辱，毕竟我们是战败了，可是一战我们是战胜国啊？巴黎和会上把国家分为三等，不同等级国家派的代表数量有差别，一等国五人，二等国三人，三等国两人。我们以为自己当然是一等国了，派了五个代表过去，到那儿一打听才发现原来是三等国。没办法，只好五个代表轮流开会。我们连当时的巴西和比利时都不如，人家一开始也被划分到三等国，后来经过“努力争取”，变成了二等国。这两件事给当时中国人的刺激太大。他们告别了过去的“天下梦”， “中国梦”开始了。<br />
<br />
“中国梦”的主题当然是“强国”。我们当时处在被欺侮，被蹂躏的状态下，必须获得自主。那么怎么强国？怎样叫强国？什么叫强大的国家呢？我们当时的一些先贤，他们站在了一个比较高的角度上来看这个问题，得出了后人看来是正确的结论。这些人，比如徐继畲、郭嵩焘、谭嗣同，他们共同的观点是：一个强大的国家，不仅是船坚炮利，不仅是财大气粗，还要政治文明，道德高尚。这样的国家才配叫强国，这样的国家才可以成为强国！（掌声）<br />
<br />
可是如何去实现强国梦呢？先贤们给出的答案是：像西方人一样实践儒家社会的影响。他们有这样的说法：世界上的强国，三代之前是中国，（对他们来说）此刻是欧美。为什么？ ――因为先人创造的文明被败家子给丢光了，被外国人拿去发扬光大了。<br />
<br />
你看西方的“虚君共和”，不正是我们孟子的“民贵君轻”么？<br />
<br />
你看西方的民主制度，不正是《礼记》里面的“选贤任能”么？<br />
<br />
你看西方议会里的议员，不正是我们“为民请命”的乡绅么？<br />
<br />
你看美国的那个华盛顿，不正是我们“禅让天下”的尧舜么？（笑声）<br />
<br />
当时说英国是“法度严明，仁义兼至，富强未艾，寰海归心”，依我看这就有点走火入魔了。那不是靠仁政换来什么“寰海归心”，殖民地都是强抢过来的。还有人说西方人礼让，守秩序，这不就是中国传统的礼教么？错。西方人是礼让，守秩序，但是和中国不一样啊：西方人礼让那是“女士优先”，中国是“领导先走”！（笑声，掌声）<br />
<br />
再说西方还有很多概念没办法套到中国的传统上来，那是西方自己创造的概念。<br />
<br />
三权分立对应什么？――三省六部？（笑声）<br />
<br />
政党轮替对应什么？――改朝换代？（笑声）<br />
<br />
宪法对应什么？――《四书》《五经》？（笑声）<br />
<br />
你说咱这判案都依靠儒家经典，判断是否违宪，法官去念“关关雎鸠”？（笑声，掌声）另外最高法院，根本没有可以对应的。所以说，当时人们对强国梦的看法，是有点走火入魔的。但是我们不应该苛求先贤。我们应该看到，在当时没有人告诉你这个路该怎么走，他们能想到这一点已经很不容易了。当我们今天向中国梦的践行者致敬的时候，要不忘先贤，不忘记他们做过的努力。（掌声）感谢你们献给先贤的掌声。<br />
<br />
马克思在《路易・ 波拿巴的雾月十八日》中写道：“人们自己创造自己的历史……是在直接碰到的、既定的、从过去承继下来的条件下创造。一切已死的先辈们的传统，象梦魔一样纠缠着活人的头脑。……恰好在这种革命危机时代，他们战战兢兢地请出亡灵来给他们以帮助，借用他们的名字、战斗口号和衣服，以便穿着这种受崇敬的服装，用这种借来的语言，演出世界历史的新场面。”那么当我们要变革的时候，我们就要回头看，鸦片战争之前我们有什么梦？中国人有什么梦？――“天下梦”。“天下梦”后来变成了“中国梦”，这两者的区别是：<br />
<br />
“天下梦”说的是人类怎么样，“中国梦”说的是中国怎么样。<br />
<br />
“天下梦”是“社会梦”，“中国梦”是“国家梦”。<br />
<br />
2、国家梦<br />
<br />
这个“天下梦”的概念又分成两个，就是“大同”和“小康”。<br />
<br />
“大同”是天下为“公”，“小康”是天下为“家”。<br />
<br />
具体体现在政权轮替和权力交接上，“大同”是禅让制， “小康”是世袭制。<br />
<br />
“大同”和“小康”最重要的区分是看权力归谁：“大同”是说天下是“公器”，天下为“公”。<br />
<br />
那么很显然，西方各种政治制度里，美国的共和联邦制，好于英国的虚君立宪制，又好于日本的实君立宪制。共和联邦最接近我国的“大同”理想。那么清王朝就不这么看了，他们认为日本的最好，国家要有一个“万世一系”的君主；维新派主张学英国，革命派就主张学美国。<br />
<br />
实际上呢，<br />
<br />
“大同”是我称之为由部落联盟向国家联盟过渡的阶段，是文明的开端；<br />
<br />
“小康”是西周到春秋这段时期，周公的理想。<br />
<br />
什么理想呢？就是由宗法、封建和礼乐构成的。宗法是社会制度，封建是政治制度，礼乐是文化制度。要做到以人为本，以德治民，以礼为序，以乐促和。<br />
<br />
“大同”是“无忧无虑”，<br />
<br />
“小康”是“有德有序”。<br />
<br />
所以孔子说“吾其为东周乎！”就是“大同”回不去了，退一步，回到“小康”总可以了吧？但是不可能。秦始皇建立了一个统一的王朝，从此“百代皆行秦政制”， “有德有序”也不行了，老百姓很难过上安生日子。<br />
<br />
那怎么办呢？开始做第三梦，“治世梦”。“治世梦”就是天下大治，风调雨顺，国泰民安，政通人和。实现“治世梦”要靠谁呢？首先得靠老天爷实现“风调雨顺”，然后“国泰民安”和“政通人和”主要依靠三种人：圣君、清官和侠客。<br />
<br />
圣君又分成两种，仁义的叫“仁君”，明智的叫“明君”，又仁义又明智的那就叫“圣君”了。<br />
<br />
没有“圣君”怎么办？还有“清官”。<br />
<br />
“清官”也没有，就只能靠“侠客”了。都希望侠客半夜入衙取贪官首级嘛。（笑声）<br />
<br />
所以你看为什么《三国》这么流行？那里面圣君、清官和侠客都齐了。刘备是圣君，诸葛亮是清官，关羽张飞是侠客。这叫“三个代表三个梦”。（笑声，掌声）但是往往就连“侠客梦”都做不成了怎么办呢？只好读武侠小说。（笑声）从“大同梦”一直做到“侠客梦”，这叫作越来越没有指望，金庸先生大行其道并非没有原因的！（掌声）<br />
<br />
到了1949年，有了一个新版本的中国梦，这个中国梦的主题是“大同”，标志性的表现是“人民公社”。人民公社这个东西――哦，不是东西。（笑声，掌声）嗯，人民公社这个 “新生事物”，（笑声）它很符合“大同”的精神。你看大同是<br />
<br />
“财货不必藏于己”――猪啊羊啊都交公了，劳动不为己，<br />
<br />
“老有所终”――有敬老院，<br />
<br />
“幼有所长”――有托儿所，<br />
<br />
“鳏寡孤独废疾者皆有所养”――有五保户。<br />
<br />
它还说“故人不独亲其亲，不独子其子”――集体生活，<br />
<br />
“路不拾遗，夜不闭户”之类就不说了。东西都交公了，自己什么都没有，闭什么户啊？（笑声）<br />
<br />
人民公社的代表就是大寨，在当时人们心目中那就是“人间天堂”。――（看看讲台）怎么没人送水？（笑声，掌声）当年有一首歌，我也会唱，讲大寨的，是这么唱的：<br />
<br />
（唱，台下打拍子，唱了一半改念歌词）一道青河水，一座虎头山，大寨就在这山下边。七沟八梁一面坡，层层梯田平展展，牛羊胖乎乎，新房一座座，炕上花被窝，屯里粮满尖。阳光满屋喜气多，社员梦里都笑声甜。<br />
<br />
――那真是“社员梦里都笑声甜”啊。不过后来我一想，这歌词里唱的不就是我们现在要建设的“社会主义新农村”么？（笑声）这个梦确实是“中国梦”，但它的源头不是儒家，是墨家。墨家讲究两个方面，一是“兼爱”，一是“尚同”。<br />
<br />
在劳动生产方面，讲究各食其力，按劳取酬；<br />
<br />
在人事任用方面，讲究各尽其能，能上能下；<br />
<br />
在社会关系方面，讲究互敬互爱，平等兼爱。<br />
<br />
（中间有一两句话漏记）村民意见不统一，就听村长的；村长意见不统一，就听乡长的；乡长意见不统一，就听大夫的；大夫意见不统一，就听国君的；国君意见不统一，就听天子的。这是“民主集中制”。（笑声）<br />
<br />
所以墨家和社会主义有相似的地方。墨家弟子都生活得很简朴，“以裘褐为衣，以F为服。”穿着粗布衣服，穿草鞋，下地干活。这墨者不就是当时的“知识青年”么？（笑声，掌声）到了汉代“罢黜百家，独尊儒术”以后，儒家就变成了“公开的执政党”，法家是“暗中的执政党”，道家是参政议政的“在野党”，墨家变成了“地下党”。（笑声，掌声）<br />
<br />
所以毛泽东应该说他采用的是道家的理论。但是毛泽东他自己不这么说。他说过一句话，说自己是“马克思加秦始皇”，是社会主义+法家思想，那这就跟晚清诸贤不一样了，他们是资本主义+儒家思想。那么毛泽东他也有梦。他的梦分成“大同梦”和“强国梦”。“大同梦”更多来自墨家，“强国梦”就不能靠墨家了，得靠法家，讲斗争。先秦诸子，墨家讲究逻辑，儒、道、法讲究矛盾。而讲究矛盾的里面，儒家讲究矛盾的统一，道家讲究矛盾的转化，只有法家讲的是矛盾的斗争。法家的哲学就是斗争的哲学，毛泽东的哲学也是斗争的哲学。<br />
<br />
三、个人梦<br />
<br />
（大意：我们做过那样的“大同梦”，但却并没有如愿以偿的圆梦，反而是十年文化大革命，付出了沉重的代价）那么我们有必要对这样的实践做一个反思。 文化大革命和“大同”都讲的是一个“公”字，但“大同”的“公”指的是公权力，文革的“公”是财产上，更是精神上的公有化。 在法家看来，个人不存在个性问题，它是工具和机器。这和《共产党宣言》里面所说的不一样，《共产党宣言》里面这样说：共产主义社会“将是这 样一个联合体，在那里，每个人的自由发展是一切人的自由发展的条件。”有一位叫做金观涛的学者曾经做过专门的检索，结果表明“民主”“共和”这样的概念很 早就被引入中国，但是“个人”“个性”这样的词直到“五四”以后才被介绍到中国。<br />
<br />
其实如果说起我们传统观念中最早主张个人的权利和自由的，我觉得是道家的杨朱――这里要做一个植入广告，你们可以去看我写的《我山之石》，那里面我 提出了一个观念，就是杨朱的理论是中国历史上第一部《人权宣言》。这样，同样是“大同梦”，道家的是个人主义，法家的是集体主义，儒家的是折衷主义。这叫 做“一个梦想，各自表述”。（笑声，掌声）<br />
<br />
说到这里我想给大家说一个我年轻时候的事情。以前中学校园流行喇叭裤，就是上窄下宽的设计，然后当时有个说法叫“问题青年”，形象就是穿喇叭裤，戴蛤蟆镜，手里提着一个收录机，一边走一边放邓丽君的歌：“路边的野花，你不要踩~~~”（笑声）。那个时候学校的老师也是非常郑重的做思想政治工作的，看到学生穿喇叭裤，品行不端，不能不管啊，他们就贴了一个标语：“喇叭裤能吹响向四个现代化进军的号角吗？”（笑声）然后有学生在底下贴了一个条： “什么裤子吹得响？”（笑声，掌声）<br />
<br />
我想说，这就是干涉个人权利和个性自由的表现。我们的先人向往美国式的民主共和，但是讽刺的是，美国恰恰是资本主义世界里最重视个性自由的国家。改革开放以来，我们获得了很多成绩，但开放最彻底，成果也最显著的地方是哪里呢？――是广东。为什么？就是因为广东对个人权利和个性自由的（加强语气）尊重！（掌声）<br />
<br />
正是这种尊重，才成就了“广东经验”，没有这样的“广东经验”，就没有这样的“南方视角”，（掌声）没有这样的“南方视角”，就没有现在的南方报业集团，也没有现在的《南方周末》！（掌声）<br />
<br />
尊重个人权利和个性自由，（加强语气）离“大同”最近！（掌声）<br />
<br />
我们做的“中国梦”，最本质是要让“人民”幸福。而我们不能忘记的是，“人民”是由无数有生命的个人组成的，是（加强语气）人格独立，意志自由的个人！（掌声）<br />
<br />
我们说要建设一个强国，那么这个强国里，我话不能随便说，衣服不能随便穿，饭不能随便吃，（加强语气）这样的强国我要它干什么！（长时间热烈的掌声）<br />
<br />
（从这里开始光顾着鼓掌了，没有记全）<br />
<br />
（大意：法家抑制人们的个性自由，贯彻法家思想的秦朝二世而亡）（加强语气）活该！（热烈的掌声）<br />
<br />
（大意：一个抹杀了个人意志的政权将会变成一台没有感情的机器，一台“杀人的机器”）（掌声）<br />
<br />
我们改革开放30年来最大的进步，就是我们有了选择的自由。我们能选择了，也敢选择了。我们经历了两次解放的过程，第一次是“五四”，让中国人可以自由恋爱，“我的恋爱我做主”；后来是改革开放，让中国人可以自由择业，“我的职业我做主”。这两个“我做主”，归根结底都是尊重了个人的意志，“我的权利我主张”，“我的事情我做主”！（掌声）<br />
<br />
我们现在的“中国梦”，就是可以不再集体做梦，可以不在同一个体制下，同一个框架里做梦，可以随便做梦，想怎么做梦就怎么做梦，甚至可以（着重）同床异梦。（掌声）<br />
<br />
我们中国人，几千年来，做了三个梦：天下梦，国家梦，（停顿）个人梦。天下梦就是大同梦，国家梦就是强国梦，个人梦就是幸福梦。这些梦是互为前提，彼此成全的。（大意：追求个人的幸福，就是为国家的强大做贡献；反过来，要想追求国家的富强，就必须保证个人的全面发展）中国的道路，不是理论问题，是实践问题。（大意：需要通过人的个性解放，实现社会的进步，国家的富强，人民的幸福。）<br />
<br />
（结束）<br />
