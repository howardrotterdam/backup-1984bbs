北大学生问「中国梦与台湾梦有没有交集？」，龙应台说可以回归到她认为的共同价值观基座上；还有粉丝提问时先表达仰慕之意，全家都是龙应台的书迷，这次全家动员来北京听她演讲。<br />
<br />
龙应台说，目前她在大陆点阅率最高的一篇文章是《不相信》，她鼓励在座的人说，年轻人应该对很多事情都抱持怀疑。她说，这次来大陆，抵达后又要增加演讲数据，网络一搜寻却发现很多网页都是「此网页已……（不存在）」，她真想将那一幕拍下来。于是很多数据只好麻烦助理从台湾找出来寄给她。<br />
<br />
大陆媒体对龙应台演讲全无报导，甚至北大网站也找不到有关消息，显示当局对龙的演讲之紧张。<br />
<br />
现年58岁的龙应台，生于台湾，是著名文化人及公共知识分子。曾多次撰文批判北京政府，其新着《大江大海：一九四九》内容因涉及国共内战，目前在大陆仍被列为禁书。<br />
<br />
龙应台曾在北京谈起这本书时称它为「和平之书」，她曾形容，大陆决策者如果真的看了这本书，应该会说「赶快出吧！」而龙应台的前一本描述亲情的书《目送》则在大陆获得出版，赵举行新书会。</div>

							
							
							
							
