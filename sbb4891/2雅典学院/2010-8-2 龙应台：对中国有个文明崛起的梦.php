<br />
吴建民：我填报志愿的时候，首先填写了北大的物理系，因为我特别喜欢物理。然后我的班主任找到我，他一开始不是建议我去学外交的，他是建议我去外语学院。说实话，当时我对外语学院没有好印象。当然，现在不一样了。<br />
