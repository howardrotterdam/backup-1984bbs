<br />
前天和昨天我参加了《南方周末》主办的“2010年中国梦践行者致敬盛典”系列活动，31号去了致敬仪式，1号下午去听了易中天和龙应台的“激荡中国：梦的解析”讲座。上一篇讲述了不太愉快的取票之后，本次首先带给大家的是第一场致敬仪式的全程记录。<br />
