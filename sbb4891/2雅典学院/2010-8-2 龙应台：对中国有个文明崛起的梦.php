																											<div id="postmessage_770134" class="t_msgfont">---------------<br />
<strong>南方周末 “激荡中国,梦的解析” 易中天 龙应台 北京大学演讲 <a href="https://1984bbs.com/redirect.php?tid=54672" target="_blank">https://1984bbs.com/redirect.php?tid=54672</a></strong><br />
---------------<br />
<br />
龙应台：对中国有个文明崛起的梦<br />
<br />
2010-08-02中国时报【王铭义／北京报导】<br />
<br />
著名作家龙应台昨在北大发表演说时强调，台湾原有的「中国梦」在经历退出联合国、台美断交后，台湾人普遍认清事实与现实，并逐渐转型为「台湾梦」；她说，她对中国是有梦的，但那个梦不是「大国崛起」，而是「文明崛起」，看待一个国家是否文明，要看它如何对待少数民族、异议分子与弱势族群。<br />
<br />
龙应台昨天下午在北大百周年纪念讲堂发表题为《温柔的力量：从乡愁到美丽岛》演说，这是龙应台闻名于华人世界廿余年来，首度获准在大陆公开正式演讲。<br />
<br />
现场近千听众大都是《南方周末》的读者，据悉，由于出借场地的北大校方态度低调，主办单位无法全数开放场地让更多听众进场聆听。<br />
<br />
龙应台这次顺利获邀在北大讲堂演说，因她刚获《南方周末》举办的「中国梦践行者致敬盛典」代表人物，其作品被评为「具有柔轫的硬度，感动的力量和思考的锋芒」，而获该殊荣。龙应台新著《大江大海：一九四九》目前仍被中国大陆列为「禁书」，但大陆多数的读者都已从网上阅读了相关篇章。<br />
<br />
主办单位为缓和邀请龙应台演说的敏感性，先安排著名电视讲堂主持人易中天，从历史脉络谈中国人的「中国梦」。易中天开场时刻意以一幅对联，形容北大讲堂盛况：「一九四九龙应台大江大海。二○一○易中天小打小闹」。不过，易中天言辞锋利，对大陆当前盛行的强国论调多所批判，不时赢得观众掌声。<br />
<br />
龙应台演说时强调，谁在乎大国崛起？如果所谓「大国崛起」，是人民引以自豪的军事耀武扬威，是经济的财大气粗，是政治势力的唯我独尊，那宁愿它不崛起。这种崛起，最终带给人民的是灾难。如果两岸在文明价值观没有共识，「血浓于水」又算什么呢？<br />
<br />
在回顾上世纪八○年代书写《野火集》心路历程时，龙应台说，她廿三岁在美国图书馆才首次接触国共内战历史，当时才知原来在台湾的教育，都是一个巨大的谎言，后来书写《野火集》，就是在报复那个大谎言。<br />
<br />
龙应台在谈到台湾卅年来的政治文化变迁历程时，特别播放余光中的《乡愁四韵》、李双泽创作的《美丽岛》，及党外人士遭逮捕的「美丽岛大审判」照片。当现场响起「骄傲的祖先，正视著我们的脚步…，我们有勇敢的人民，荜路蓝缕以启山林…」歌声时，台下更有听众轻声跟著习唱，气氛感人。<br />
<br />
龙应台说，台湾走过的民主课程，政治犯曾变成掌权者，掌权者也会变成反对者，当年标榜道德的大审判辩护律师更沦为贪污嫌疑犯，但这些「摔倒」经验是台湾必要的民主课程。她强调，不论蓝绿，都不要被这些表象所惑，大陆也不要以为台湾的民主只有「乱」。<br />
<br />
她强调，「台湾梦」虽有分歧，但不论蓝绿都有共识，也有基本的价值观：国家是会说谎的、掌权者是会腐败的、反对者是会堕落的、政治权力不是唯一的压迫来源；资本的权力可能一样的压迫。她说，因为权力侵蚀是无所不在的，所以个人权利、言论自由，是要每个人寸土必争，绝不退让。<br />
<br />
北大校园演讲结束前，龙应台在回答一女学生问到她个人现在梦想是什么时，龙应台不加思索说，「我希望找一位情人跟我去旅行！」同时，希望她的儿子安德烈不要嫌她太烦人，现场听众抱以温暖的掌声。<br />
