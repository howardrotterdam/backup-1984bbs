													<h2>龙应台北大演讲吁文明崛起　．安同</h2>
						
						
																											<div id="postmessage_773733" class="t_msgfont">龙应台北大演讲吁文明崛起　．安同<br />
 <br />
亚洲周刊<br />
<br />
龙应台在北京大学百年纪念讲堂演讲，以《文明的力量――从乡愁到美丽岛》为题，揭示台湾「中国梦」的破灭与嬗变、及民主化的艰辛历程，期待中国大陆也以文明的力量崛起。 <br />
--------------------------------------------------------------------------------<br />
<br />
八月一日下午，北京烈日当头。可以容纳一千八百人的北京大学百年讲堂座无虚席。<br />
<br />
舞台右侧一角，一袭白衣的龙应台用字正腔圆的国语开场：「第一次接到（《南方周末》）电话，希望我谈谈『中国梦』的时候，我的第一个反应是：『一千枚飞弹对准我家，我哪里还有中国梦啊？』」<br />
<br />
台下笑声一片，接着是泪水，为这次演讲的难得，为她提及的颠沛流离到台湾的同胞，也为她讲出了许多大陆人想说而不敢说的真话。<br />
<br />
前一天下午，同样的地方，《南方周末》将「二零一零中国梦践行者」奖杯颁给了龙应台，在接受大陆艺术家陈丹青提问时，她的每句话都让现场观众捏了一把冷汗。她说：「我的一个梦想，就是这个国家，能够不再有不能出版的书，不能上映的电影。」<br />
<br />
掌声空前，陈丹青对着镜头调侃说：「东方卫视还是把这段删掉吧。」<br />
<br />
在七位践行者中，龙应台是唯一来自台湾的，只要看过她作品的人，都一定很认同这段她被致敬的理由：龙应台先生的作品具有柔韧的硬度，感恩的力量和思考的锋芒，她在完成自己梦想的同时，持续关切着弱小者的梦想，其文字不动声色地传扬着普遍性的价值。<br />
<br />
柔韧的硬度，依然贯穿演讲始终，主题说的是《文明的力量――从乡愁到美丽岛》。能够到「共匪」统治的土地上公开演讲，她一度难以置信。但也不改批判本色，只是不再像当年批判国民党那样针尖对麦芒，而是幽默地含沙射影：昨天在北京怎么也搜不到这张美丽岛审判的照片，一搜就跳出一个提示：「此网页已不存在」，我真想把它拍下来；很想跟大家合影签名，但是很抱歉，北大给我的时间有限，不然就要断水断电……<br />
<br />
进场时，很多人捧着龙应台的书凑到她面前请她签名，都被工作人员一一挡了回去，她没有作声，只是微笑，起身，鞠躬致歉。<br />
<br />
真正让听众动容的是一首首熟悉而陌生的歌曲――《反攻大陆》《乡愁四韵》《车过枋寮》《龙的传人》《美丽岛》，龙应台试图用一九四九年以后的台湾歌曲演变，告诉大陆听众：台湾人的中国梦如何在被世界抛弃的孤独中越变越小。<br />
<br />
罗大佑演唱的余光中诗歌《乡愁四韵》在大陆传颂最广，现场很多人都跟着哼唱起来：「给我一掌海棠红啊海棠红，那血一样的海棠红，那沸血的烧痛是乡愁的烧痛……」<br />
<br />
龙应台问台下的听众：「你们知道海棠红是什么意思吗？」舞台中央的大屏幕上出现了一张地图，龙应台说，「这就是海棠红，是中华民国的地图」。台下掌声响起，惊叹连连。印象中的雄鸡怎么多了好一大块？原来那是外蒙古。她开始讲述上一代的中国梦：<br />
<br />
「一九四九年，近两百万人突然之间被残酷的内战连根拔起，丢到了一个从来没有去过、甚至很多人没有听说过的海岛上。在战火中离乡背井，颠沛流离到了岛上的人，思乡之情刻骨铭心，也是无比真诚的。那份对中华故土的魂牵梦绕，不是『中国梦』吗t」<br />
<br />
龙应台祖籍湖南衡阳，她说她的父亲十五岁走出衡山参加了国军，母亲则是在浙江新安江畔长大：<br />
<br />
「我的父母那代人在一种『悲愤』的情结中挣扎着，我这代人在他们乡愁的国家想象中成长。但是支撑着这个巨大国家的想象下面，有一个基座，垫着你、支撑着你，那个基座就是价值的基座。<br />
<br />
它的核心是什么？台湾所有的小学，你一进校门门当头就是四个大字：『礼义廉耻』。进入教室，简朴的教室里面，墙壁上也有四个大字：『礼义廉耻』。如果一定要我在成千上万的『格言』里找出那个最最基本的价值的基座，大概就是这四个字。」<br />
<br />
她引述《论语泰伯篇》的「士不可以不弘毅，任重而道远」，指出「士」在中国的价值和意义：<br />
<br />
「我初中一年级的国文老师叫林弘毅，数学老师叫陈弘毅。同时期大陆很多孩子可能叫『爱国』、『建国』，我们有很多孩子叫『弘毅』。我们都是要『弘毅』的。<br />
<br />
对自己要期许为『士』，对国家，态度就是『以国家兴亡为己任，置个人生死于度外』。这是蒋介石的名言，我们要背诵。十一、十二岁的孩子背诵的就是这样的句子，用今天的眼光看，挺可怕的，就是要你为国家去死嘛。」<br />
<br />
不过，龙应台紧接着指出，在「国家」之上还有一句：<br />
<br />
「为天地立心，为生民立命，为往圣继绝学，为万世开太平。（张载）<br />
<br />
饶有深意的是，虽说以国家至上，但是事实上张载所说的是，在『国家』之上还有『天地』，还有『生民』，它其实修正了国家至上的秩序，因为 『天地』跟『生民』比国家还大。」<br />
<br />
龙应台说，就是这个价值系统，形成一个强固的基座，撑起一个「中华大梦」，但这个中国梦在一九七零年代出现了质变。：<br />
<br />
「一九七一年中华民国被迫退出联合国，台湾人突然之间觉得自己变成了孤儿。可是，最坏的还没到，一九七九年一月一号，中美正式断交，这个『中』指的是当时的中华民国，也就是台美断交，中美建交。长期被视为『保护伞』的美国撤了，给台湾人非常大的震撼，觉得风雨飘摇，这个岛是不是快沉了。在一种被整个世界抛弃了而强敌当前的恐惧之下，救亡图存的情感反而更强烈，也就在这个背景下，原来那个中国梦对于一部分人而言是被强化了，因为危机感带来更深更强的，要求团结凝聚的民族情感；大陆人很熟悉的《龙的传人》，是在那样的悲愤伤感的背景下写成的。这首歌人人传唱，但是一九八三年，创作者侯德健『投匪』了，歌，在台湾就被禁掉了，反而在大陆传唱起来，情境一变，歌的意涵又有了转换。<br />
<br />
七十年代整个国际情势的改变，台湾的『中国梦』开始有分歧。对于一部分人而言，那个『海棠』中国梦还真诚地持续着，可是对于另外一部分人就不一样了。」<br />
<br />
龙应台认为，在台湾的中国梦开始转换时，有一首「里程碑」式的歌――《美丽岛》：<br />
<br />
「李双泽，跟很多台湾年轻人一样，一九七零年代发现台湾不能代表中国，而且逐渐被国际推到边缘，在危机感和孤独感中，年轻人开始检视自己：为什么我们从小被教要爱长江、爱黄河、歌颂长城的伟大―那都是我眼睛从来没见过，脚板从来没踩过的土地，而我住在淡水河边，怎么就从来不唱淡水河，怎么我们就不知道自己村子里头小山小河的名字？台湾也不是没有大江大海呀？」<br />
<br />
台湾里程碑式歌曲<br />
<br />
「青年人开始推动『唱我们的歌』，开始写歌。那个『中国梦』显得那么虚无飘渺，是不是该看看脚下踩的泥土是什么样？他写了《美丽岛》，改编于一首诗，一下子就流行起来，大家都喜欢唱。《美丽岛》真的是代表了从中国梦慢慢地转型到『站在这片泥土上看见什么、想什么』的『台湾梦』里程碑……」<br />
<br />
最不为大陆读者熟悉的《美丽岛》却引起了最大的共鸣。当胡德夫的钢琴伴奏声响起，现场顿时静了下来，从舞台往下看，不少人红了眼眶。<br />
<br />
从《美丽岛》的歌曲开始，龙应台轻轻地一转，就把『中国梦』转到了台湾的民主转型上：「从海棠叶的大中国梦慢慢过度渡到台湾人脚踩着泥土的小小的台湾梦，人民在七十年代末八十年代初开始问『我是谁』。八十年代后，台湾两千多万人走向转型，自我感觉就是越来越小，什么事情都一步一个脚印，一点一点做。所以，台湾人就一块儿从大梦慢慢转到小梦的路上来了，开始一起上八十年代的民主大课。这个民主课程上得够辛苦。」<br />
<br />
龙应台指着「美丽岛大审判」的历史照片，让大家猜那位站着咧嘴笑的年轻人是谁？几乎获得了异口同声的响应：「施明德。」<br />
<br />
令龙应台最为感慨的是，仅仅三十年，当年公开为政治犯辩护的「英雄」陈水扁成为腐败分子，政治犯成为掌权者，而掌权的国民党后来成为在野者。她不同意别人说，台湾的民主是「乱」象：台湾民主的「乱」是必上的课；因为只有真正跌倒了，才真正知道要怎么再站起来，跌倒本身就是一种考试。<br />
<br />
掌声再次响起，甚至淹没了她下面的话：<br />
<br />
「回到基座上的价值观来看，从前的中国梦慢慢被抛弃了，逐渐发展为台湾的小梦，然后一起上非常艰辛、痛苦的民主课，然而台湾不管是蓝是绿，其实有一个非常结实的共识，比如说：国家是会说谎的；掌权者是会腐败的；反对者是会堕落；政治权力不是唯一的压迫来源，资本也可能一样的压迫；而正因为权力的侵蚀无所不在，所以个人的权利、比如言论的自由，是每个人都要随时随地、寸土必争、绝不退让的……<br />
<br />
这是大多数台湾人的共识。你所看到的争议、吵架，立法院打架，其实都是站在这个基础上的。这个基础，是以共同的价值观建立起来的。」<br />
<br />
这样的共识对于没有经历民主洗礼的大陆人来说，显然有些陌生。一个小时的演讲中，龙应台就这样一次次地挑战大陆人的认知边界，也在一次次挑战大陆当局的容忍底线。而演讲最后对「大国崛起」的剖析，更让一些人感到如芒在背：<br />
<br />
「我倒是很愿意看到中国的崛起，可是我希望它是以文明的力量来崛起的……一个国家文明到哪里，我看这个国家怎么对待外来移民，怎么对待它的少数族群。我观察这个国家的多数如何对待它的少数――这当然也包含十三亿人如何对待（台湾的）两千三百万人！<br />
<br />
谁在乎『大国崛起』？至少我不在乎。我在乎的是刚才我所说的文明刻度――你这大国怎么对待你的弱势与少数，你怎么包容意见不同的异议分子，这，才是我在乎的。如果说，所谓的大国崛起，它的人民所引以自豪的，是军事的耀武扬威，经济的财大气粗，政治势力的唯我独尊，那我宁可它不崛起，因为这种性质的崛起，很可能最终为它自己的人民以及人类小区带来灾难和危险。<br />
<br />
谁又在乎『血浓于水』？至少我不在乎。如果我们对于文明的尺度完全没有共识，如果我们在基座的价值上，根本无法对话，『血浓于水』有任何意义吗？那是古老的、落后的、过时的理论。」<br />
<br />
演讲不止被掌声打断，龙应台对大陆当局充满黑色幽默的解构似乎总能引起读者的共鸣。龙应台也说到她有没有『中国梦』：那要看你对它怎么下定义了？如果你是指政权的梦，那我没有，因为在我眼里，国家、政权只是一个组织或者公司，而且它还可能撒谎。但是，如果把它定义为这片土地上的人民、社会，那么我当然有，而且强烈地希望他们能够幸福、自由。」<br />
<br />
按照大陆目前的言论尺度，她的演讲比较出位，但很多人认为，能让其到大陆领奖本身就已经说明中共对她是默许的。<br />
<br />
「她是作家、记者，更是一位很好的演说家和布道者。」不止一位在现场的观众向亚洲周刊对龙如是评价。尽管主持人建议大家不要摄像，但还是有很多人照摄不误，也有听众担心错过大屏幕上的「反共」言论，对着录音笔，轻声念了起来，更多的人在用手机拍照上传到微博。<br />
<br />
搜不到的还有她的《大江大海一九四九》，最后提问环节，没有人问起过这本书，也没有人问起《请用文明来说服我――给胡锦涛先生的公开信》，「因为真正知道的人不敢问，问问题的都是不知道的。」一位现场媒体人说。促使龙应台动笔写这封信的是，共青团下属的《中国青年报》的冰点周刊在二零零六年一月被勒令停刊。这天她再次表达这样的期盼：<br />
<br />
「我深深盼望见到的，是一个用文明尺度来检验自己的中国。这样的中国，因为自信，所以开阔，因为开阔，所以包容，因为包容，所以它的力量更柔韧、更长远。当它文明的力量柔韧长远的时候，它对整个人类的和平都会有关键的贡献。」<br />
<br />
末了，她鼓励二十多岁的年轻读者要敢于「不相信」，不相信权威，不相信说教，不相信现实的合理存在，但更重要的是找那个相信的东西。并引用她一九八六年八月十一日离台赴欧前说过的一段话作为结束：<br />
<br />
「今天晚上站在这里说话，我心里怀着深深的恐惧，恐惧今晚的言词带来什么后果，我的梦想是，希望中国人的下一代可以在任何一个晚上站在任何一个地方说出心里想说的话，而心中没有任何恐惧。我们这一代人所做的种种努力也不过是希望我们的下一代将来会有免予恐惧的自由。」<br />
<br />
有观众问她的梦是什么，她说：「找个情人去旅行。」或许只有这样小小的梦想，才能对抗大大的恐惧吧。■</div>

							
							
							
							
