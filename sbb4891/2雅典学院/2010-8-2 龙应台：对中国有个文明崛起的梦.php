<br />
王克勤：（前略）……我们有新闻管制，这是中国特色。（热烈掌声）<br />
<br />
王克勤：中国有很多年轻的记者，年轻也有干劲，但年轻往往又意味着幼稚，我们需要一些足够成熟和老练的专业记者来处理中国问题。（掌声）<br />
<br />
