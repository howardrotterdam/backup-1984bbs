													<h2>中国网民掀起新一波使用真名实姓倡议</h2>
						
						
																											<div id="postmessage_683117" class="t_msgfont"><a href="http://www1.voanews.com/chinese/news/china/no-fear-real-name-internet-20100614-96281053.html" target="_blank">http://www1.voanews.com/chinese/ ... 00614-96281053.html</a><br />
记者: 黎堡 | 香港 2010年 6月 14日<br />
<br />
中国网民掀起了新一波使用真名实姓的倡议。他们说，这是公民消除恐惧、畅所欲言的第一步。维权律师和网民们说，使用真名实姓的维权人士能更好地得到同行们以及公众舆论的保护。<br />
<br />
在中国，因言获罪的案件层出不穷。北京的刘晓波、成都的谭作人和宁波的张建红都因为发表了批评政府的文章而被判煽动颠覆国家政权，并被关进监狱。许多网民在网上发表文章时因为担心被当局打压而隐匿自己的真实姓名。<br />
<br />
*公布真名是消除恐惧的第一步*<br />
<br />
北京艺术家艾未未去年曾因为声援谭作人调查四川地震灾区豆腐渣工程而遭到成都公安人员的殴打和拘禁。不过他认为，隐匿姓名并不能避开当局的清查，相反，公布真实姓名和身份，则是消除恐惧的第一步。<br />
<br />
艾未未说：“这是必要的一步。当然你不走这一步，你可以永远地匿名。但我认为一个人如果只指责当局，只指责政府是不够的。每个人的回避，每个人的掩盖，和这种怯懦，才造成极权的傲慢。我们都需要公开地谈话，透明地执政，透明地来谈话。如果我们不鼓励这些东西，仅仅生活在某种恐惧，而且很多时候我认为这种恐惧被夸张，那么这将是无济于事的。”<br />
<br />
*网民对真名行动反映强烈*<br />
<br />
星期天，艾未未在社交网站推特上发起了“张开嘴，说出你的名字”的真名活动。他说，网民对这项活动的积极反响令他感到吃惊。<br />
<br />
艾未未说：“我们从中午、下午开始做，做到晚上十二点的时候，可能有六、七百人吧。我们还在统计。这个已经很让我吃惊。很多年轻人，很多有家、有孩子的人都说我们没有什么可以害怕的。他们就把名字写出来。”<br />
<br />
浙江省杭州市的何士林响应了这项倡议，并将自己的名字和电话公布在网上。<br />
<br />
何士林说：“我本人主张公开姓名、电话和地址，用我的实际行动去感染身边的人，因为现在很多人不愿意透明真名。”<br />
<br />
*使用真名容易得到民众关注*<br />
<br />
成都维权人士谭作人的律师浦志强说，他理解很多人对以真名实姓写文章心有余悸，但是现在是放下恐惧包袱的时候了，因为有了真名实姓，人们遇到麻烦后会得到更多人的支持和援助。<br />
<br />
浦志强说：“你不用真名，谁都不知道你，但是政府想知道你的时候还是一下子就能知道你，你防备不了公安的网监。它会找到你。更重要的是，你因为不用真名，你不太容易得到民众或者网友的关注和支持，因为大家不知道你是谁。”<br />
<br />
四川省遂宁市的余跃波在《零八宪章》上签过名，他说，自己的名气不大，如果更多的人知道他的姓名和身份，他会有更大的安全感。余跃波还说，过去有一些秘密维权人士在被拘捕后受到了当局残酷的打压，可是外界很难帮助他们。<br />
<br />
余跃波说：“这些秘密维权甚至组党的人，一旦被抓，因为外界不知道他们，他们没有什么影响力，被抓后被判的刑非常重，对他们的镇压也特别的残酷。”<br />
<br />
*角马俱乐部也曾倡议真名实姓*<br />
<br />
艾未未不是近年来第一个发起使用真名实姓行动的倡议者。2008年，来自四川、山西和河南的几位公民发起成立了一个以公开身份站出来呼吁政治改革的角马俱乐部。这个网上交友平台的几位发起人从非洲角马面对凶残的鳄鱼临危不惧横过马拉河的场面中得到启发，认为中国需要更多的人勇敢地站出来，面对邪恶，才有希望达到繁荣和自由的彼岸。角马俱乐部发起人之一侯丙才说，使用真名实姓便于网友们更多、更深地交流。<br />
<br />
侯丙才说：“如果你连真名都不讲出来的话，那还有什么意义呢？我在网上就用真名。如果连这点都做不到的话，至少朋友们很难熟悉起来，很难互相找到。”<br />
<br />
不过，侯丙才说，在某些情况下，比方说在网上发表非常敏感的文章，使用匿名也可能是必要的。<br />
<br />
两年来，在角马俱乐部签了名的各地民众受到了当局不同程度的威胁，其中一些人被国保人员找去谈话，一些人还因为家庭成员受到压力而被迫删除签名。<br />
<br />
*艾未未：当局抓不了所有人*<br />
<br />
艾未未说，要求维护公民权利的确是一件有风险的事情，因此他并不要求网民响应他的真名实姓呼吁，但是他说，中国社会也在进步，民众应该对自己国家的未来有信心。<br />
<br />
艾未未说：“当局怎么可能抓每一个人呢？这是不可能的嘛。如果说每个人都公开说出自己的观点，怎么可能(都抓)呢？ 我觉得当局不是铁板一块吧。我觉得任何人，当他看到大多数人都有这个意图的时候，他也会尊重民意一些。”<br />
<br />
曾经蹲过监狱、被国保多次谈话的上海维权人士冯正虎说，对付恐惧的一个好办法是做好思想准备。现在他的住所外每天都有四、五个人监控他的行踪，而他在门边一直放着一个打好包的旅行袋，随时准备被当局带走。</div>

							
							
							
							
