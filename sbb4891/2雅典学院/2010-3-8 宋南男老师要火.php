<br />
 <br />
<br />
&nbsp; &nbsp;以前每回夜里在他车里听着他接老婆的电话用种种谎言搪塞时时，就会设想如果自己是那女人并知道了真相就究竟会如何？能听出他的不自然的谎言么？会猜想到他与别的女人在一起么？会象我一样听说他在三亚国际小姐选美大赛期间去桑拿找小姐后一阵瞎想并妒意丛生么？---现在想来，我其实与赵一样的可怜，甚至，还不如她，起码，他还是不得不最后回到她身边！我算什么呢？不过是一块旧抹布，给扔一边，再也想不起来了！<br />
<br />
 <br />
<br />
&nbsp;&nbsp;马姐劝我说想开点，说今天这样的结果不是我的错。但我知道我错了，我的良心上也过不去。对不起的人是除了赵和她女儿培英，最对不起的，是我的H，那么多年来，他为我付出那么多，对我一直那么迁就，给予那么多信任，无私给了最多的爱，把我当最亲的亲人---我知道，再找不到这样真心对我的人了，可是我却骗了他那么多年，伤了他那么多，最后连我们的小家家，也最终分蹦离析---我可以想象，他知道这一切真象后，没有疯掉，就已是奇迹，对于他，我只有永远的忏悔！<br />
<br />
 <br />
<br />
