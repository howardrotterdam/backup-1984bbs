宋南男，宋南男，宋南男...............这个男人的名字，不想再提起，但它却永远不可能忘记了！ 我会让他受到应得的惩罚的，我发誓！<br />
<br />
&nbsp; &nbsp; <br />
<br />
&nbsp; &nbsp; <br />
<br />
&nbsp; &nbsp; 妈妈当时说的没错，总要到关键时候，才能看到一个人的真实面目。而这个男人在还没到“关键”的时候，已让人看到最为丑陋的本质。<br />
<br />
&nbsp; &nbsp; <br />
<br />
&nbsp; &nbsp; 只是他所谓爱我的单纯，实际上不过是利用了我在情感生活上的“傻气”罢了：当年入学考试中的帮忙，我很清楚，本质上不过看上了我的年青和姿色，其背后的心态，这几年中，我都居然忘了去揣度到底有多么之卑鄙了--拿他自己承认的几个有染的女生来数，可见他利用这种主考之便，“潜规则”过的女生，又何止我一个？只不过，别人都可能只是“一次性消费”掉，而我却被“充分性”地开发了！ <br />
<br />
&nbsp; &nbsp; <br />
<br />
&nbsp;&nbsp;有回他在闲聊中还说起：在早期当团委书记时，有个女生，为了得到留京指标，主动半夜找到他的筒子楼房间彻夜未归，结果他全力为其促成了她的幸福---与这样的女生相比，我是幸？抑或是不幸？<br />
<br />
 <br />
<br />
&nbsp;&nbsp;他的前两次离婚经历，足以说明他的问题--这样的男人，哪个女人遇着，可能都会是倒了八辈子的霉！可怜最憎恨第三者的赵在38岁最终遇到的男人，会在结婚半年她刚刚怀上孩子时，即已在床上说对我说：如果我离开她，你会离开他么？---这样的男人我也曾那么死心踏地的爱过，现在想来简直让人恶心！--我还起码对他老婆表示过对不起，对他幼小的女儿培英表达过我最真诚的抱歉。可是这个男人他做了什么？他要的是年轻的女人的身体和满足他那永难得到满足的兽欲！在上了我家的床、占有我那么几年后，他在最后关头居然记挂着的只是他那些利益！并为了利益，要以牺牲我作为代价！<br />
<br />
