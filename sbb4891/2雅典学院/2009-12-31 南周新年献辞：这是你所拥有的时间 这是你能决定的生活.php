																											<div id="postmessage_407158" class="t_msgfont">【南方周末新年献辞】<br />
新世纪已经过去十年了，我们并不想神话什么数字，我们要说的是，时间里包含着一些东西。<br />
我们也不想神话某一个时间节点，正如十年前全世界的媒体都去追逐新千年的第一缕晨曦，仿佛不知道太阳每天都会升起、日子每天都是新的一样。但是我们总是要找一个机会向你表达问候，所以因袭十年来的传统，在新年来临之际，祝愿阳光打在你的脸上。<br />
我们也没有必要神话阳光。阳光下有美丽的春暖花开，也有罪恶的杀人越货。我们借助阳光和鲜花来给自己打气。我们是那么脆弱，但是我们必须坚强。<br />
我们也不要神话坚强。英雄固然令人敬仰，但是芸芸众生大多是普通人。物价降了我们就高兴，工作丢了我们就犯愁。遇到坏人我们会害怕，警察来了又怕他不守法。就像花朵依赖春天一样，我们依赖这个社会。有时候我们的眼睛是雪亮的，但大多数时候我们不明真相。给点阳光我们就会灿烂，房子拆了只有极个别会去自焚。我们生活在一个平凡的世界里，期待最平庸的人也能得到幸福。<br />
我们还是来说说十年吧。十年时间足可以诞生一代人。他们被称为00后，会被贴上一些标签。不要神话这些标签，他们和父辈一样会生老病死，一样面对社会的善恶美丑，一样需要勇气和担当，也一样会徘徊于软弱和坚强。他们不是拯救我们的天使，我们也没有理由扮演魔鬼。<br />
这十年来经济腾飞，国力日盛。国内生产总值当年不到十万亿，如今已经超过三十万亿。我们是第三贸易大国，但是我们占第三十以后的选项也还不少。十年来建了那么多房子，也拆了那么多房子。当然，有人提醒说要看到建设是社会主流，那是千真万确的事实。但是每一条支流都不能抛弃，每一个个体更值得关注。每一个人的快乐都不可替代，每一个人的痛苦都渴望理解。<br />
十年前我们正在争取加入世贸组织，还不知道可以举办奥运会和世博会，如今我们已经在G8会上一言九鼎，在哥本哈根出演主角，在东亚地盘上一枝独秀。但是不要神话大国崛起，我们小时候就听说周总理在国际舞台上叱咤风云。按照过去的思路，崛起意味着担当，我们能否引领人类文明？想想未来的变化，民族国家一定会有新的发展，我们总会明白一些基本的道理：大舅二舅都是他的舅，大国小国都是我的国。<br />
十年前互联网的泡沫还没有吹起，一些今日忙碌奔波的大人物还闲得发慌，整天往模样单薄的BBS上灌水。今天我们已经可以手机上网，博客遍地开花，twitter一往无前，网络购物前景无量，网民反腐也所向披靡。但是不要神话互联网，没有网络的时代仍然建立了新中国，世界上的民主与自由大都要比网络出生更早。网络是我们生活不可分割的一部分，它可以是黄色的，也可以是红色的，还可以是绿色的，也可以是多姿多彩的，想要什么样的生活取决于我们自己。<br />
十年来我们经历了太多的灾难。从SARS到汶川地震，从松花江水污染到三鹿奶粉掺毒，从拉萨和乌鲁木齐的暴力事件，到全国各地的矿难，一次又一次地，总有一种力量让我们泪流满面。不要神话灾难，它们就是灾难。有人说真高兴我们总算挺过来了，但是不要忘记那些没有挺过来的人，还有那些正在挺着甚至永远都只能挺着的人。<br />
亲爱的读者朋友，下一个十年又要开始了……没错，这个说法没有意义。我们只是想要告诉你，这是你所拥有的时间，是你生命中无可逃避的又一段历程，假如你愿意的话，你可以用它来做很多事情，你可以找到你想要的生活。<br />
