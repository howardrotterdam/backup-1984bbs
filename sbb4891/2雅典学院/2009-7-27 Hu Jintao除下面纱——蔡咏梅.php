													<h2>Hu Jintao除下面纱――蔡咏梅</h2>
						
						
																											<div id="postmessage_180795" class="t_msgfont">Hu Jintao正在除下面纱<br />
&nbsp; &nbsp;Hu Jintao两年来的表现，尤其在摆脱Jiang Zemin的阴影之后，已逐渐除下神秘面纱，露出一个强硬共产党首领的凶恶面目。 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 二○○二年四月底正在中共皇权储位上的Hu Jintao访问美国，美国朝野以国家元首的高规格接待他，除了烧政治冷灶，也是想近距离看看中国这位未来领导的真面目，传媒都在问「who's Hu？」（Hu Jintao是谁？）Hu Jintao究竟是甚么东西？今天已经比较清楚。 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 据悉，该年小布什访华时，美国务院给他阅读的中共领导人资料，Jiang Zemin、朱F基都是密密麻麻几十页，非常详尽，但有关Hu Jintao则只有寥寥几行，可见美国对他完全不了解。但即或Hu Jintao访美后，美国务院档案中的数据，相信也未增加多少。对美国，以及对我们中国人来说，Jiang Zemin阴影下的Hu Jintao当时是一个很神秘人物。 <br />
<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 人一阔，脸就变 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 不过尽管神秘，但表面的Hu Jintao还是顶亲切温和的。即使对一个小记者远远的喊话，Hu Jintao也会尽量挤出微笑，不停点头响应，说「我现在赶时间，不能够接受采访，不好意思。」使小记者们十分感动。 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 今年九月Hu Jintao终于摆脱太上皇Jiang Zemin的阴影，正式登上九五之位，党政军大权独揽一手。胡的神秘面纱徐徐除下。这时那个亲切谦虚的接班人不见了，人们看到的是一个凛然不可冒犯的独裁者。访南美，新皇的御前走狗警告香港记者，不准向胡主席提问题，谁问取消谁的采访权。结果，一路上记者们噤若寒蝉。胡主席则面如冷霜，与一直尾随的记者无一次眼神的交接，完全当记者透明无物，与两年前访美时的Hu Jintao竟然是两个人。 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 鲁迅说，「人一阔脸就变」，此即「权力的傲慢」的活脱脱例子，亦使人再次感悟「绝对的权力绝对腐蚀人」的千真万确。 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 今天大权在握的Hu Jintao全面镇压传媒，抓捕异议人士以「铁腕立威」，震惊海内外。其实也可说不令人意外。在此之前，韬光养晦的Hu Jintao也会偶尔露狰狞。 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 二○○二年Hu Jintao访美时，曾闹出一件尴尬事，只是当年的解读不同。Hu Jintao与美国众议院领袖会面时，开初气氛很好，待到向来严厉批评中国人权状况的民主党众议员佩洛西提到中国人权状况时，Hu Jintao立刻板起了脸，一句话也不说，也不接受美国众议员递交给他的四封人权抗议信，搞得气氛难堪到极点。美国人极之诧异，只能解释为这位中共接班人既胆小谨慎又缺乏随机应变的智慧。现在看来很清楚，这位年轻时在共产党专政机器中专司洗脑之职的政治辅导员，洗人脑时亦被洗，满脑子僵化冷酷的共产党意识形态，对西方人权理念相当仇恨，当时的情况属自然反应。 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 剿杀传媒，镇压异己 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 其实无需等到四中全会下旨剿杀异己传媒和文化人，在此之前，Hu Jintao已多番亲自操刀，铁腕镇压异己。已知的至少有以下数宗。 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 一、拉萨戒严，镇压藏族人民。一九八九年三月八日国务院宣布拉萨戒严，可以说是随后八九民运北京戒严June Fourth屠杀的先声。当时西藏自治区党委书记Hu Jintao头戴钢盔，与戒严部队杀气腾腾出现在拉萨街头。有报导称，是中央听了Hu Jintao汇报和建议后才作了拉萨戒严的决定。在拉萨事件中，Hu Jintao的角色完全是后来北京事件中的LiPeng。正因为Hu Jintao敢于镇压人民，政治忠诚经受住了考验，所以才有资格被选作接班人。Hu Jintao的皇冠上有藏族人民的鲜血。 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 二、迫害政治学者刘军宁。如果大家都不健忘的话，应记得二○○○年一位北大学生投书光明日报，未点名批判刘军宁到该校演讲宣扬自由主义这一事件。刘军宁并因而丢掉公职，被他任职的社科院政治学所除名。但很少人知道下令开除刘军宁的幕后黑手即是当时政治局常委中分管意识形态和中国社科院的Hu Jintao。 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 当时那个打小报告的学生是向教育部告状，教育部查明被告者是著名学者刘军宁，将此作为高校动态上报中央。在政治局会议上Jiang Zemin出示此信说，「教育要改革，但也要注意方向，北大这位研究生的报告值得重视。」Jiang Zemin讲话尚留有余地，但Hu Jintao处理起来却毫不留情，立即批示社科院领导，「像这样的学者怎么会还能留在社科院工作？」并指示御用传媒公开此信示众。因此才有刘军宁失公职及光明日报投书事件。 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 可笑当时知内情者都苦心为Hu Jintao开脱，说胡是被Jiang Zemin逼着，迫不得已，是挥泪斩马谡。 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 据悉最近刘军宁发出抗议说，「Jiang Zemin主政十三年我的网页没有被封过一次，可是这两年我的网页已被封过无数次了，有没有人能带话给胡总书记，请他不要这样迫害我。」可见当初Hu Jintao并非是迫不得已。　 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 三、迫害中共党内开明派、前中宣部长朱厚泽。Hu Jintao打压刘军宁是他作为中共党工的阴暗意识形态的本质使然，对朱厚泽的迫害则更多是出于狭隘心理的公报私仇。 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 一九八五年朱厚泽调北京当中宣部长，Hu Jintao继任他的贵州省委书记职位。遂不免有人对前后两任贵州地方官政绩加以比较。 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 了解情况的人说，朱厚泽是个有民主理念，择善固执，而又具才干和魄力的政治家。文革后复出于一九八三年任贵州省委书记管经济，该年贵州工业产值就增长百分之十八，全省财政获得极大改善，被视为奇迹。而朱厚泽上述长处Hu Jintao均无，调到贵州后唯一突出的就是常到下面四处视察，做出一副深入基层的亲民状，但论政绩则乏善可陈。由于前任口碑太好，Hu Jintao不免相形见绌，心里不是味道。一九八七年朱厚泽因任中宣部长主张政治宽松在反自由化时落马，胡在贵州即借反自由化之名趁机整了朱厚泽一些旧班底。到Hu Jintao当上总书记，对朱厚泽更是处处防范睚必报。 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 去年七月一批党内外民主派学者在青岛举办了一场民间修宪论坛，有朱厚泽参加，Hu Jintao秋后算账，指控朱厚泽是黑手，指示中央速发三份文件，指控民间修宪活动是反党反社会主义，点名十人，第一人即是朱厚泽。并动用国安部搜集材料，企图坐实朱厚泽黑手之名以论罪。所幸青岛会议召集发起人曹思源挺身出来澄清，说明朱厚泽实际仅为被邀嘉宾，而且朱本来不打算参加，犹豫再三，到开会前三天才决定与会，说他是黑手完全不符事实。但曹因此被全天候监控达半年之久。Hu Jintao气量之小，由此可见。 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 四、下令逮捕维权记者赵岩。 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 九月十六日纽约时报驻北京记者助理赵岩被捕。赵岩原任《中国改革》杂志编辑，以专门帮助社会弱势者争取权利而著名，人称维权记者。赵岩被捕原因是当局怀疑是他泄漏Jiang Zemin将在九月十九日中共四中全会下台的消息（纽约时报九月七日率先报导），Hu Jintao龙颜大怒，亲自批示「要抓泄密的人」，赵岩因此失去自由，并在十月二十日被控「非法提供国家机密罪」受到起诉。虽然纽约时报一再澄清消息并非由赵岩提供，但Hu Jintao钦定御案，赵岩看来凶多吉少。 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 六亲不认，僵化顽固 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; Hu Jintao混迹中共官场如鱼得水，生成了典型的双重人格，需要时可以很有亲和力，但实际冷酷寡情。进中南海后为保住接班人地位，六亲不认，甚至置其从小养大的孤独养母于不闻不问，十几年未去探望她。当地政府要拆养母住房，养母抗议说是Hu Jintao住过的房子希望保留。当局请示北京的Hu Jintao，他不施援手，非常不近人情地说「你们要拆就拆，房子不是我的，是她的。」评论说，如此「共产党式的矫情，」只有老奸巨滑的周恩来可比。 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 而这次Hu Jintao下令要封杀的一位政治老人，曾有恩于Hu Jintao，但Hu Jintao并未手下留情。 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 显见Hu Jintao这位邓小平指定的中共权力隔代接班人，不过是又一个独裁者，Jiang Zemin是技术官僚，而Hu Jintao是党棍，意识形态比Jiang Zemin更僵化、更顽固，对自由主义思想更仇恨，打压异己手段更铁腕。Hu Jintao露出庐山真面目，是再次提示人民，中共已是朽木不可雕之党，民主化的希望已与此党绝缘。</div>

							
							
							
							
