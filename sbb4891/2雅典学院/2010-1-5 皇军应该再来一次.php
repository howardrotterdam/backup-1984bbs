 <br />
习惯性无助效应 <br />
<br />
奥菲米尔和西里格曼做过一个这样的实验： <br />
<br />
狗可以越过屏障或者通过其他积极的行动来逃避实验者的电击。并且，有很多狗会狗急跳墙的对电击者发起反抗性的攻击行为。但是，如果狗在不断的受到不可预期的――无法预测的、随时随地的、无法控制的电击之后，狗的行为会发生突变。这个突变，被称之为：习惯性无助效应。 <br />
<br />
什么是习惯性无助效应呢？就是当这些狗被反反复复地多次随机的电击之后，狗会突然的放弃任何可能有机会的逃离。并且，再也没有狗急跳墙的本性了。 <br />
<br />
之后，奥菲米尔和西里格曼又把这个的实验用到人类社会。经过研究和考察，人类也有狗的这种“习惯性无助效应”。人一旦由于被独裁政府、流氓集团无休止的、随机的狠狠打击之后，就会产生习惯性无助效应。这时候的人，即便有很多机会和压迫者对抗，也有很多机会可以逃离，但由于他们的无助感和对压迫者的深深的恐惧，会莫名其妙的放弃一切努力。甚至于，任何其他人的对抗和努力，他不但不同意反而把这些对抗者看成是傻瓜、白痴。这还不止，他还要主动的为压迫者助威。 <br />
<br />
这时候，无论你是反抗者，或者是反抗者的声援者，又或者是反抗者的鼓动者，都被他们一律说成是罪人。或者是试图害人的人。在阳痿的国度里，任何不是阳痿的人，都是罪人。 <br />
<br />
<strong>支那人的很多家庭之中，父母就给小孩“电击疗法”，像驯狗一样驯服小孩，结果是小孩长大了进入社会就会服从统治。 </strong><br />
<br />
这样的民族，就不会有任何人愿意创新探险。任何的科技的人文的地理的，创新探险都不会有了。每个支那人，就像一片菜皮，被扔入泡菜坛子，再被捞出来。支那就像个泡菜坛子，永远没有变化。当代支那的表面是经济繁荣，物质丰富，内心还是嬴政刘邦那个时代。 <br />
<br />
<strong>幸好，我是越受电击，越是叛逆。当我回到南京，看到所有的亲友以及满街的行人的眼中都没有神采，脸上都没有笑容，行尸走肉一般，只有在聚餐时才会流露出贪婪的眼神，我觉得皇军应该再来一次。 </strong></div>

							
							
							
							
