													<h2>要自由，要人权，要法治，要宪政，但是不要民主</h2>
						
						
																											<div id="postmessage_503706" class="t_msgfont">当前的形势下，中国人民争取权利的过程中，要自由，要人权，要法治，要宪政，都有一点点改善的希望。但是要民主，是一点希望都没有的。把民主和“自由、人权、法治、宪政”捆绑，最后是连累那些东西都没有希望了。<br />
<br />
民主只是解决权力来源的一种方式。却不等于自由，人权，法治，宪政。<br />
<br />
宪政解决公权力的界线。但是民主要解决的是权力的来源。<br />
<br />
我不认可没有民主就没有自由、人权、法治、宪政的说法。也不认为民主了，自由、人权、法治、宪政就会有了。<br />
<br />
我反对把民主混淆为自由、人权、法治、宪政。这样不仅争取不到民主，还葬送了自由、人权、法治、宪政的希望。<br />
<br />
共产党怕的是失去权力。要民主，就是把共产党作为最大的敌人。那些宣扬清洗共产党的，宣扬对党员和党官的仇恨的，实际上是帮助共产党团结起来。清洗的只能是制度，不能清洗人。如果你宣传把靠独裁制度掠夺的资产剥夺回来重新分配，惩处那些靠不正义手段霸占掠夺的财产，结果是必然导致<br />
<br />
但是，我不相信共产党会这么容易失去统治。我既不相信正义的力量，也不相信人民大众的力量。我更相信共产党的力量。<br />
<br />
在继续支持共产党一党执政的情况下，争取自由，人权，法治，宪政，在权力不再这般令人迷恋、失去权力不再如此令人恐惧的情况下，才是争取民主的时机。<br />
<br />
所以，我提倡：要自由，要人权，要法治，要宪政，但是不要民主。</div>

							
							
							
							
