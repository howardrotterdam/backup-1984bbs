最近出版的图书为杂文集《可爱的洪水猛兽》和合集《草》，前者是发表过的杂文的合集，后者是以前小说的一些片段语言的精选，没有做什么宣传是因为怕读者误会是全新的图书。杂志已经全被准备妥当，包括第二期和另外一期副刊也已经开始准备，一直没有出版和发行是因为一些程序上的的原因，因为在我们国家，公民虽然拥有出版的自由，但政府也拥有不让你出版的自由，并且后者拥有解释权，所以大家请不要着急，我并不是放大家鸽子，也不是故意钓大家胃口，我没有必要每个月承受办公的成本而故意不出。相信会在一个月以内合法上市。<br />
