影片告诉我们，想要搞定女人，还得靠房子。尤其在上海。<br />
在中国，这部中庸的中性的献给中共的由中影拍摄的影片，就给一个中间的分数，5分。<br />
 <br />
《第九区》是一部极好的外星题材电影，也叫《贫民窟的百万外星人》，它的出现使得食物链得以运转，白种人欺负黄种人，黄种人欺负黑种人（事实上是白种人直接欺负黑种人，黄种人则喜欢互相欺负），黑种人欺负外星人，外星人再欺负白种人。给9分。<br />
 <br />
《大内密探零零狗》说明香港最近的喜剧已经彻底失去想象力，尤其是打大陆市场的喜剧。从周星驰以后，这类非常容易出彩的历史题材喜剧一直没有出现过喜剧，最主要的另外一个原因是导演坚持要自己写剧本，而他们依然停留在他们的年代里，并且随着年龄的增大，能力也降低了。随随便便抓一个网络写手来写剧本，都要比如今的王晶和菩提老祖自己写的强。当然，每个人都有不同的审美，如果看了这部影片笑了两次以上的人请不要购买和阅读我的作品。<br />
这部电影给0分。<br />
