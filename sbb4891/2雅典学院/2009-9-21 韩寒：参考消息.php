最近也看了不少的影片，笼统给大家做一个介绍，最感人最好的一部就是飞屋历险记。这是好莱坞的主旋律片，所谓没文化的好莱坞，它的主旋律永远散发着人性的光芒，所谓有文化的中国，它的主旋律永远散发着政治的恶臭。《飞屋历险记》也提供给了中国公民一个思路，因为众所周知，我们的房产只有70年的土地使用权，那过了七十年以后怎么办呢？理论上房子还是你的，但是土地不是你的了。《飞屋历险记》和政府各给了我们一个解决方法，好莱坞告诉我们，只要用气球把房子吊起来就行了。中国政府告诉我们，经过了严格质量控制以后，我们的房子一般不到七十年可能就塌了，所以不用考虑那么多。<br />
给9.8分。<br />
 <br />
《建国大业》从影片的制作和表演上讲还算上乘，从影片的类型上来讲应该算是幻灯片。我很怀疑导演是在用反讽的手法来拍摄这部电影。看着那些开国元帅们纵情唱着国际歌，毛泽东说，我们永远要团结。我看到了这部电影成为一部真正的优秀电影的潜质――那就是它不在1949年结尾，而是以1976年结尾。在1949年的时候，诚挚的人民诚挚的望着诚挚的毛泽东和诚挚的新中国，然而六十年后，人们依然没有一米自己的土地，大家都是寄生而已。以前我们的旧社会由很多的阶级组成，现在简单了，只有四个阶级，那就是穷人，房奴，富人和富豪。 <br />
