													<h2>刘荻的罪证：自己的命运要掌握在自己手中</h2>
						
						
																											<div id="postmessage_685417" class="t_msgfont">著名律师高智晟曾经接到过这样一个电话，他说：“今天有一个人打来电话，他把声音变了，就是那种变音的设备，咱们就不清楚他是谁了。他说高智晟，我的声音必须采取变音措施，如果不采取变音措施的话，公众都知道。我说这又是大人物，他哈哈一笑，他说我们坚决支持你，坚决支持你，他说：你持续的在骂我们，他来了这么一句，但是，你是我们的老朋友，你不知道我是谁，我知道你是谁。听这话也是体制内的。他说我关注你就是因为你持续的在骂我们。我说你在鼓励我继续骂。他说骂的好。”<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;几个月以后，高律师就被捕了。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;无独有偶，不久之前发表的中国泛蓝联盟两名成员的对话也有如下内容：“估计两党有故意拿泛蓝在做什么试验！”“也有抬举泛蓝的感觉，有意让泛蓝成为一个新的有味道的可以充当缓冲的黏合剂！”“我听说的一个小道消息，胡的家宾就有国民党的说客。”“团派，年轻，泛蓝；胡锦涛；反腐败，新文革；十七大，和谐社会，政治体制；你可以联想嘛？”“胡要利用年轻人搞一次自己的新文革！”“不要在意被利用，也不要在意被谁利用，只要可以推动一个体制的变化和文明。”<br />
<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;泛蓝将来的命运如何，我们还不得而知。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;最近类似的事情还有“未来中国论坛”的“军中声音”，声明军队要发动政变，希望网友提供建立民主政府的具体举措和主张。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;这几个例子都有一个共同之处：有些民运人士和网友在与魔鬼作交易。魔鬼象征这样一种力量：它是强大的，但又是未知的，是你所无法控制的；如果你要与这种力量打交道，你只能被它控制。一个匿名电话，听说的小道消息，网上的几个帖子，不是来自“高层”，就是来自“军队”，貌似背后有强大的力量，但实际上你对它一无所知。如果你把自己的未来和希望完全寄托在这种靠不住的“力量”上，让这种力量来控制你，支配你，就等于是在与魔鬼作交易，把自己完全卖给对方了。真的是与魔鬼作交易吗，其实也不尽然，那些人可能还不如魔鬼呢。从民间传说中我们可以得知：基督教中的魔鬼其实是一个言而有信的绅士，当魔鬼与人签订出卖灵魂的契约时，从来没有人怀疑过魔鬼的信用。但以上几个例子中，那种来自“高层”、“军方” 的力量其实什么也没有承诺，什么责任也不用负。就拿“军中声音”来说，其实他只说了“不排除今年六中全会期间采取行动宣布某省或地区暂时独立的可能性”，后来又说 “今年不行的话，明年还有两会、十七大，后年还有机会。”事实上他什么也没有承诺。而如果国内的网友听信了他的这些话，真的提供了什么“建立民主政府的具体举措和主张”的话，那么对不起，今后你的命运就不是自己所能掌控的了，至少定你个颠覆罪是没有问题的了。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;这个网友可能还在盼望军事政变呢，但是对方没有承诺任何事。把自己的命运完全交到对方手里，对方出卖起你来是没商量的，据说高律师在被捕前，心里其实还是相信自己不会被捕的。从中共建政后的历史上来看，体制外力量企图利用或介入中共内斗的，从西单民主墙到八九六四，再到最近的高智晟，无一有好结果。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;上面说的是不要被权力所左右，下面谈谈不要被非理性的力量所左右。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;非理性的力量，这里主要谈谈投射。投射就是把自己内心中的情结、原型等等投射到对象身上。比如郭国汀律师说高智晟是“英雄、伟人和超人”，这就是典型的投射：郭国汀把自己内心中的“英雄”、“救世主”等原型投射到了高智晟身上。投射是非理性的，因为原型和你的投射对象之间可能有很大的差距，但是投射又很难避免，甚至可以说人与人之间的交往很大程度上都是在互相投射。投射有时会给投射者和投射对象造成伤害，例如投射破灭后的失望，有时投射者在投射破灭后还会对原来的投射对象进行伤害，甚至谋杀。有时投射也会给投射对象造成很大的压力，这个我有亲身体验：刚从看守所出来的时候人人都把我当成女神一类的角色，我觉得和我本人差距特别大，我都觉得自己不是自己了。现在我心理平和多了：你把我投射成女神，最后投射破灭了，那是你的问题，不是我的问题。我就是这个样子，我没有义务满足你的期望。还有一种可能是投射对象把别人对自己的投射内化了。比如高智晟，如果人人都认为他是“英雄、伟人和超人”，可能最后他自己也会认为自己是这样的，但这也会造成许多心理问题。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;荣格曾经解释过他的一个病人的梦。这个病人40多岁，出身寒微，靠奋斗当了一所学校的校长。近来他患了一种病，眩晕、心悸、恶心，衰弱无力，类似瑞士的高山病。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;荣格解释了他的梦后说：不要忘了你已从小村庄走到了校长位子。有如一个登山者，你一天爬到了海拔6000英尺，已经累坏了，不要想再“往上爬”了。荣格说：你产生高山病症状也正是这个原因。荣格还说，这个病人不属于那种在6000英尺高度上生活的人，他属于这个高度以下的人。这样他不再患神经症，只是处境要低下些。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;高智晟恰恰也是那种出身底层，靠自我奋斗取得了很大成就的人；他的雄心大得很，还想继续往上爬，这时人家把他当成“英雄、伟人和超人”，他当然乐于接受；但他忘记了这种高度已经超出了他的能力，在这种高度上他已经无力决定自己的命运了，而只能任人摆布。因此，屈服于非理性的投射，从而失去了自我，也是把灵魂卖给魔鬼的一种表现。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;上述两点有着共同的主题，即要把命运掌握在自己手中，既不能把灵魂交给权力，也不能交给非理性的力量。我们或许不能完全左右局势，也不能完全决定未来（但也绝非完全不能），但我们自己的命运一定要由自己来决定。无论局势和未来如何变化，我们都要自己掌握自己的命运，只有这样我们才能够在一定程度上按照自己的希望来创造未来。<br />
&nbsp; &nbsp; <br />
