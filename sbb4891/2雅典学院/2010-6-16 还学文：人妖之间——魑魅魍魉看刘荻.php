													<h2>还学文：人妖之间――魑魅魍魉看刘荻</h2>
						
						
																											<div id="postmessage_685390" class="t_msgfont">文章摘要: 围剿高智晟之役，当自刘荻起。戴着心理学者的假面，挥着“非理性”的板斧，以科学的名义要把高智晟打成神经病。这种经授意启发的自觉，是刘荻进阶魔鬼的资本。造谣、中伤、影射和恫吓，是不加掩饰的下流；而这种披着学术外衣借刀杀人，则是一种经过包装的恶毒。 <br />
<br />
<br />
作者 : 还学文, <br />
<br />
<br />
l表rg：2/18/2007<br />
<br />
自愿效忠的刀笔吏<br />
<br />
 <br />
<br />
“从高智晟的悲剧谈起――自己的命运要掌握在自己手中”，这样一篇不入流的文字，不值得认真对待。所谓高智晟的一通电话，所谓泛蓝成员的对话，……居然都是用引号括起，可谓煞费苦心。书面文字中以引号标示的文本，要么确实可信，那么必要有文献为据；要么是虚构，例如在小说当中，那么不必当真就是了。<br />
<br />
这种无中生有、故作神秘的杂凑，别有用心、恶语伤人的影射，如角落里窜起的阴风，如黑暗晃动的魑魅魍魉，太明显是有意而为，是要毁誉于人。这样的文字或是他人-捉刀刘荻代笔――用刘荻自己的话说，“和魔鬼打交道”，或她心领神会，自告奋勇，总之都是甘为刀笔吏。要是这种无耻文字还会有什么“风光”，自然也不是笔者的功劳。不过有了这样的线人文字、刘荻这个人就彻底地当掉，一文不值了。<br />
<br />
围剿高智晟之役，当自刘荻起。戴着心理学者的假面，挥着“非理性”的板斧，以科学的名义要把高智晟打成神经病。这种经授意启发的自觉，是刘荻进阶魔鬼的资本。造谣、中伤、影射和恫吓，是不加掩饰的下流；而这种披着学术外衣借刀杀人，则是一种经过包装的恶毒。<br />
<br />
 <br />
<br />
心理分析不是科学<br />
<br />
-――刘荻错打了主意<br />
<br />
 <br />
<br />
刘荻打出心理学招牌，也确实困扰了读者，在对科学的衷心服膺和对刘荻诡秘阴险文字的疑惑之间，读者无所适从。<br />
<br />
自然科学例如物理化学，人称严密科学，它们的理论是普遍有效的。例如经典物理学的万有引力定律，既适应于描述天体之间的重力关系，也适应于解释地球上重物之间的关系；它既能够严密地解释既往――已经发生的，也可以准确地预见未来――尚未发生的；而由此推演出来的结论，可以在自然、试验和经验中重复验证。我们日常所说一种学说或理论是“科学的”，即是指这种普遍有效性和经验可确证性的品格。<br />
<br />
而心理学，是关于心理现象的一门学科，却还不成为上述意义上的一门科学。且不说对基本心理对象和心理现象心理学尚无物理学对物理对象和物理现象那种严格的、定量的、系统的和普遍有效的描述，并且现今众多所谓心理学概念与理论大都是在心理分析/治疗过程中并作为对症下药的分析/治疗方法产生和提出的。这些概念和理论，基本上是指向特定的心理现象及其之间的关系而提出的尝试性的推测和假说。作为心理分析/治疗的方法，它们可能有效，但不是一定有效：对此一病例有效，未必对彼一病例亦有效；对此一病人曾经有效，未必定对同一病人未来仍然有效。参与心理过程的因素、从而心理过程或说心理现象常常是不可重复的。鉴于心理现象的这种特性，并且考虑到尚不能完整而准确地确定参与形成心理现象的全部因素，尚不能严格地描述心理现象的生成，心理分析/治疗概念理论的这种局限性，自然就不足为奇了。 <br />
<br />
因此，心理学还不具备作为科学所必要的理论的普适性及其经验可验证性。从某个心理分析/治疗解释――或者本身仍由争议，或者已经过时而被淘汰――出发，视之为放之四海而皆准的原理，去论断社会现象，本身就是站不住脚的。此其一。<br />
<br />
 <br />
<br />
追杀高智晟的“理性”罗网<br />
<br />
――刘荻作茧自缚的“投射”心理学<br />
<br />
 <br />
<br />
祭起“投射”这个概念，刘荻以为就能代人把高智晟一群一举击溃而万无一失。<br />
<br />
前面提到，迄今为止心理学的概念和理论基本上是心理分析/治疗的概念和假说，相对于具体的心理现象有严格限定的解释和应用。而“投射”又基本是一个对病态心理分析的概念，它的理论正当性和有效性是限于极为确定的病态心理过程以及对它的分析与治疗。把这样一个病态心理分析的概念拿过来，就要解说和断言一般的社会行为，本身就是没有根据的，此其二。<br />
<br />
至于她的那些论断的谬误，无须专业的心理学训练，只需翻开一本心理学词典，找到“投射”词条，就一目了然了。<br />
<br />
关于“英雄投射”，刘荻的“理论”诊断如下：<br />
<br />
-&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;投射，即“把自己内心中的情结、原型等等投射到对象身上”，典型的如郭国汀“把自己内心中的‘英雄’、‘救世主’等原型投射到了高智晟身上”。<br />
<br />
-&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;投射是非理性的，原因在于原型和投射对象之间的差距。<br />
<br />
-&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;投射会导致非理性或灾难性后果：例如投射破灭后的失望，会引发对投射对象的伤害，甚至谋杀。<br />
<br />
-&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;投射内化，即投射对象把别人对自己的投射内化，例如高智晟，“如果人人都认为他是‘英雄、伟人和超人’，可能最后他自己也会认为自己是这样的”。<br />
<br />
但是，刘荻的每一个论断都站不住脚。<br />
<br />
 <br />
<br />
一、“投射”之为正常的心理过程，刘荻无的放矢的“非理性”宣判<br />
<br />
 <br />
<br />
“投射”，心理分析/治疗理论定义为一种一般的心理过程或者一种综合性的防卫机制；它是人内在心理的外化、向外部世界的推移，表现为内心愿望、需求、幻想等向外部世界的人(个体/群体)、物、情境无意识的转移。前者是正常的，后者是病理的，通过投射外化而驱除那些反感和厌恶的愿望、需求以及幻想等，而得以解脱。<br />
<br />
显然，投射与非理性全无关系：这种心理过程是无意识的，因此，无论对于投射主体、还是过程本身，均无理性或非理性可言，只有常态与病态的之分。<br />
<br />
作为一种正常的心理过程，不少心理分析/治疗家反对使用“投射”这个概念。他们认为把这种外化的心理过程解释为投射，就否认了人的主观感知能力的正当性。作为一种意识状态，不会有非主观的感知、纯客观的感知。任何感知都渗透着主观色彩。于是心理学家荣格提出并且强调“积极”投射，并指出，它是移情心理的重要机制。而移情心理对于艺术创作和艺术欣赏之必不可少，则是人所共知的，谁能称之为非理性呢？另有心理分析/治疗家主张，只有对那种引发主体对外部环境调适的干扰而导致病态的心理转移，才宜使用“投射”概念。<br />
<br />
至于刘荻以原型与投射对象之间的差距作为投射非理性的根据，更是明显的自相矛盾。内在心理与外部对象之间的差异是投射可能发生的条件，若是没有分别，两者同一，投射便无从发生。<br />
<br />
 <br />
<br />
二、什么是“典型”的投射？刘荻把她的“英雄投射”封为典型是为什么？<br />
<br />
 <br />
<br />
刘荻称郭国汀把自己内心中的“英雄”、“救世主”等原型投射到高智晟身上为典型投射，而这是又不确的。<br />
<br />
根据心理学词典定义，投射是指某种愿望、需求、幻想等向外部的人、物或情境上的转移，即个别特性向具体对象身上的转移。<br />
<br />
刘荻所谓的内心人物对象向外部人物的转移，如果称为典型，则是一种病态投射的典型，典型地出现于心理病患作为投射主体与他的心理分析/治疗者的二元关系之中。对此，心理分析/治疗理论称之为“投射认同”，即病人把自己所缺少、追求和渴望的情感、愿望和对象，或自己所厌恶的感情、需求、冲动投射到他的心理分析/治疗者身上，期望在投射对象身上得到满足，或借控制甚至摧毁投射对象以达到驱逐和战胜那些所反感、所厌恶、要驱逐的心理，对投射对象的伤害即是在这种病态投射下可能发生的。<br />
<br />
即使是对这种病态的心理过程，现代的心理分析/治疗家当中也有人反对用这种投射概念来解释。他们认为，这种解释实际上是心理分析/治疗家对与病患关系的干扰与失败的一种推卸责任。<br />
<br />
刘荻硬要把这样一类心理分析/治疗中明确定义的病态投射，典型化为某种她杜撰出来的正常心理投射而加以鞭笞，自是别有用心：让她杜撰的投射者陷入病态，再用她病态的投射者去恐吓作为她文章打击的目标-――高智晟作为投射对象，一箭双雕。<br />
<br />
 <br />
<br />
三、“投射内化”――应特殊需要专门生产的理论哑弹<br />
<br />
 <br />
<br />
抛出一个所谓“投射内化”，说“英雄投射”对象的高智晟把别人对他的投射内化，自以为英雄。这堪称刘荻最荒诞不经的胆大妄为了。<br />
<br />
与“投射”对应，心理分析/治疗理论中有“内投射”、但没有“投射内化”的概念。作为彼此对立的概念，“投射”与“内投射”描述外化与内化两个彼此相反的心理过程。如果投射是一种外化，那么它的内化不就将“投射”、从而将被投射的心理现象抵消于无形了吗？如果按照刘荻的说法，“投射内化”是被投射者对于他人投射的内化。那么请问，“投射”作为一种主观心理过程，高智晟如何能够与投射者没有直接的关联的情况下中感知、了解和确定他人有如此这般内在的心理现象，又如此这般地投射到他的身上，并且在全然无知的情况下，为了哪般，还能将其内化到自己心中呢？<br />
<br />
与投射类似，“内投射”也是一种心理防卫机制，这种心理过程发生于现实的二元关系――心理病患与他的分析/治疗者、“我”与父母、某个主体与他的性对象――之中。在这里，主体接受和内化的不是作为整体或是部分的外在的对象，而是在主体以往的经验中可能与这个对象联系起来的某种特性、某种职能。同样的，描述这样一种病态心理过程的“内化”概念，与“投射”一样，也不能应用于在此之外任意的心理或社会现象。<br />
<br />
 <br />
<br />
至此，刘荻“投射”立论的所谓“科学”基础，瓦解罄尽。读者诸君不必再为救高智晟先生于宵小刘荻的神经病诊断而紧锁愁眉了。<br />
<br />
 <br />
<br />
推心置腹劝刘荻<br />
<br />
 <br />
<br />
至于那一段以心理学家荣格名义的论述，逻辑上完全不经推敲。置高智晟于死地心切，终于图穷匕首见，什么“神经症”、“处境低下”的影射，什么“出身底层”、“往上爬”的冷嘲，都是撕破了脸的肆无忌惮的羞辱与穷凶极恶的叫骂。<br />
<br />
当我第一次读到刘荻这篇无赖文章时，惊异于她的嚣张，遏制不住几乎冲口而出，“刘荻，悠着点儿！”，我想到的是，柏林墙倒后，对于前东德国安部门以及为其效劳的特务、线人历史的清理，这个工作今天还在继续。最近，准备提升为华沙大主教的波兰神父，因为给共产党作间谍的历史被揭露出来，被迫引退，波兰开始了对共产党时代国安部门的监控以及为它效忠的奸细历史的清理, ……<br />
<br />
刘荻你还年轻，这些，你一定有机会亲历。自然，不仅仅是刘荻。<br />
<br />
 <br />
<br />
还学文，二零零七年二月十八日，德国</div>

							
							
							
							
