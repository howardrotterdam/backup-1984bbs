老菜 <br />
<br />
广场最西端，五、六辆威风凛凛的坦克一字儿排开，使帝国主义和北京市民胆寒的炮口朝西，捍卫着血红的天安门。府佑街口，是三三两两的市民和鏖战了一夜的士兵平静地、甚至有些亲切的交谈。呆在北京电报大楼下的中共“建党办公室”前，我在想，一切都结束了。邓公挽回了面子。但他如何向历史、向世界交待？人类史上的屠杀多多，但极少有这样的在光天化日之下，在全世界的摄像机前。而且镇压的对象是几百万，几乎是全部的北京市民，全国的大学生和整个年青的一代！不过，还有出路。他可以在事态平定后，宣布激进的民主改革，让一个大的功绩去掩盖一个大的罪恶。他一时气糊涂了，但，到地是个明白人。<br />
<br />
那时候，这样去替邓公思考的NB兼SB满街都是。不相信会开枪，“人民军队爱人民”信条的作用倒在其次，大家尤其认为，对邓公来说，根本就没这个必要。在北大三角地，人们经常听一位中年教师的评论。有一次听他说：邓如果收回他的4，25讲话，既人民日报4，26社论，借口说是听了假汇报，然后像1978、79年那样大刀阔斧，反腐败，搞政治改革，他就是一位中国和世界历史最伟大的人物。如此简单清晰，他怎么会派军队开杀戒，遗臭万年呢？在场的人人点头称是，又信心百倍。<br />
<br />
正这样想着邓公在开枪后可能的下一步，突然，巨大的呼喊从对面传来，几乎将我扑倒。“邓叉叉，杀人犯！”“绞死李叉”“血债要用血来还”的口号将清晨的宁静撕得粉碎。抬头望去，无数绝望、衣着肮脏的大学生从六部口涌出。接着是坦克巨大的轰鸣声。身边的市民一片躁动，几位狂叫着“挡住坦克，挡住坦克”，疯狂地往前冲。先是“达达达”的枪声和“嘭嘭嘭”的催泪弹的爆炸声，接着是“打倒了两个”的喊叫。<br />
<br />
回头刚看到倒在地下的市民，眼睛、喉咙便是一阵刺痛。这催泪弹的质量真是棒，得赶快找水龙头。突然，（那时的“突然”特别多）身边美丽的女士发出一阵难听的怪叫：“啊，啊，坦克压死人了，坦克压了一大片！”奋力张开眼，看到一辆坦克似乎是惭愧地正从尸体堆上缓缓后退。履带下，在脏兮兮的绿色深蓝色的男尸中间，一个也脏，但仍然是天蓝色的裙子一瞬间将我眼睛刺得生痛。<br />
<br />
一个小伙子在尸体中寻找着，过一会，他平静、轻松地托着一位伤者，也可能是他已经死亡的朋友，远去。另一位狂叫着向我们奔来：“13个，压死了13个学生！”<br />
<br />
那辆坦克归队了。望着长安街对面的那堆尸体，尤其是蓝色的裙子，没有愤怒没有恨。只有至今回想也感到可怕的一种奇怪的平静。在一个疯狂的世界里，“生命可贵”真是屁话。它是深秋的一片败叶，一文不值，一碰就落，一落一地。有什么可大惊小怪的！有时想，当初也死去多么好，轻轻松松，玩似的。现在多糟糕，就怕死。<br />
<br />
打性觉醒开始，就爱上了穿连衣裙，尤其是穿天蓝和白色连衣裙的女孩。特别是，89年五月的北京街头，偶尔会看到MM的即兴演说。哦，年轻的朋友们，多遗憾，今天车展厅、选美中的性感辣妹算什么！你们没见过，真正自由的、在北京街头演说的年青MM有多美！特别是我在前门附近看到的那一位，天使一般的圣洁、文静，羞涩却坚定。我就想知道，6月4日那天她穿的可是蓝色连衣裙？<br />
<br />
坦克履带下的蓝裙子，在眼前晃动，在脑子里飘荡，已经二十年了。爱漂亮的小丫头，你是谁？在天堂里我也一定要找到你，告诉你我的思念我的爱。。。</div>

							
							
							
							
