													<h2>分析一下宝马车4次碾压儿童案真相</h2>
						
						
																											<div id="postmessage_830114" class="t_msgfont"><a href="http://v.news.163.com/video/2010/9/K/0/V6FK6QOK0.html" target="_blank">http://v.news.163.com/video/2010/9/K/0/V6FK6QOK0.html</a><br />
<br />
首先，视频见此处。<br />
貌似现在到处都在叫嚣故意杀人云云，或者撞伤不如撞死云云。<br />
其实这司机还真不是故意的，虽然来回碾了四次。<br />
下面分析一下。。<br />
首先看一下撞倒前的画面：<br />
<img src="http://i54.tinypic.com/2rpedy8.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
为什么车后面一大活人司机就看不见呢？<br />
看下面这两张图：<br />
<img src="http://img.carschina.com/uploads/090917/1253153423_4ab19a8f0a7b9.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<img src="http://img.cheshi-img.com/product/1/1072/4b832d3bc725c.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
X6的后方视线非常差，，腰线高，后窗很小，倒车是个大问题，明明就一SUV，非往跑车靠，把SUV原有的大空间和视野良好的优点毁掉。<br />
再看一下沃尔沃C30的后窗是什么样：<br />
<img src="http://www.jdcsww.com/jdceditor/uploadfile/20080808095302516.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
对比很明显了。<br />
后窗视野差，小孩子的身高还不到后车灯，想必车主又比较依赖电子设备，可偏偏这个时候，由于小孩子投影面积过小，倒车雷达没有反应。<br />
于是，第一次碾压发生了，左后轮压上小孩身体，车轮在小孩身体上停顿了一下，估计是以为压到了路障，脚下一给力，过了。。。这时候小孩子还没死，在车下有踢打动作，相比还有哭喊。车主肯定已经意识到压了人了。<br />
但这时候司机开始犯SB了，感觉车轮压倒异物第一反应应该是停车，下车查看。可他挂了D档。这里是唯一存在故意碾压可能性的环节，但可能性也不大。一般人在倒车撞人之后，下意识的第一反应都是朝前开回去，把人让出来。（如果此时他继续倒车，那就基本是故意无疑了。）<br />
于是左后轮第二次从孩子身上压了过去。。。此时孩子还在挣扎。<br />
<br />
然后最匪夷所思的第三和第四次碾压发生了。<br />
司机赶紧停车下车查看，可是刚一开车门，车子继续倒车。。。在无人驾驶的情况下左后轮、左前轮依次完成了第三、第四次碾压。<br />
这也是被认为故意杀人最明显的证据。<br />
但实际情况是怎么回事呢。<br />
再来看两张图。<br />
一般自动挡车子的档把是这样设计的：<br />
<img src="http://pic.xcar.com.cn/img/news/j2fZGsfyAe0330.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
机械档把，司机可以很直接的了解当年车子到底在什么档位上。<br />
再看X6的档把。。。。<br />
<img src="http://newsxml.cnool.net/newspic2/2008/2008-1/2008-1-29/633371980503281250.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<img src="http://photocdn.feelcars.com/20080707/Img200196102.JPG" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
这个是X6的排挡杆，大家看一看，看出什么问题么？<br />
简单解释一下。。。<br />
宝马的电子排挡杆，是非常脑残的设计，当然其他车的电子档把一般也很脑残。我以前开过一次就差点出事。<br />
一是它的换挡是没有手感的（机械的就有手感），D、N、R几个挡位，你只能通过仪表盘或排挡杆上的小灯显示，因此经常挂什么档都不知道。最脑残的设计就是“P”档，一般车挂P挡，把排挡杆推到最前面就行了，但宝马的挂“P”挡，必须按下顶部的“P”按键，而且要等一下，它才挂上。<br />
因此第一次开这种车或者慌乱中，最容易犯的错误就是――把挡把往前一推，就下车了。这时候车会突然启动的，吓死你。如果是X6混合动力，即使是挂错挡，打开车门后车就自动锁止了，但很明显这辆车不是。。。<br />
这样情况就基本明白了吧。<br />
车子本来是D档，司机踩刹车后，由于对电子档把不熟悉。按照常规机械档把的经验用力向前一推，想要挂到P档停车，可偏偏挂到了R档。。。司机下车后发现车子再往后倒还试图用手去抬。。。<br />
<img src="http://i51.tinypic.com/mafdds.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
将近2T重的车人怎么可能抬得动。。。于是第三次、第四次的碾压也就发生了。。。司机赶紧上车熄火，车才停下。<br />
<br />
司机的责任很大，但绝没到故意杀人那份上。判个3、5年也算公道，毕竟这是交通肇事，主观上并没有恶意。但现在这么一炒，也就不好说了。<br />
PS：土方车撞人从来没见这么多人激动。。。<br />
主要原因还是安全意识不到位，心理素质差，外加车况不熟。<br />
<br />
事情的真相大体就是这样。。。<br />
开车还是多注意吧，不管多熟悉的地方，起步、倒车前一定下去看看前后左右的情况。<br />
毕竟人在车上，一个不小心就是一条甚至几条生命。<br />
