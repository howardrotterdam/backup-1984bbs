这原本是个很简单的意外情况，只不过对象换成了小孩。悲剧谁也不愿见到。首先说家长责任，在小区停车区域内玩耍都是比较危险的，而小孩出时之时没有任何家长监护，不论在哪，都是绝对脱离不了监护责任的，家人在这方面本身就是失去了监护条件，因此家人的责任在其中。司机不应免责，这是其中一个方面。 <br />
　原本抱着一副网络暴民的态度准备也向这个为富不仁的宝马贵族司机讨伐一番的我，今天恢复了理智，原因是看到了事情发展的过程。新闻描述的很详细，在此不做更多的描述，在网络上无数被冲昏头脑喊打喊杀的人，民愤又一次被激起了。而且媒体成为了添油加醋的舆论导向，失去了公正，失去了理智，同一些没有思维的人一道成为了杀人利器。 现在社会上这样的新闻数不胜举，期间媒体起到的作用几乎举足轻重。拥有一个良好的舆论导向是新闻媒体最起码的责任。倘若这个X6司机被判死刑，那么杀死他的将不会是中国法律，而是失去理智的暴民和新闻媒体，法律的威严和效力荡然无存。<br />
<br />
这原本是个很简单的意外情况，只不过对象换成了小孩。悲剧谁也不愿见到。首先说家长责任，在小区停车区域内玩耍都是比较危险的，而小孩出时之时没有任何家长监护，不论在哪，都是绝对脱离不了监护责任的，家人在这方面本身就是失去了监护条件，因此家人的责任在其中。司机不应免责，这是其中一个方面。<br />
<br />
那么我就主要说明司机的责任问题了，因为这才是大家关注的所在，很多人都只是抓住几个问题不放，那么就让我们理智来面对这些吧。<br />
<br />
1，倒车时为什么没有发现小孩?这是很多人争论不休的问题。本人这样的车也开过，那么视线的问题，在上车开车门的那一时间里向后扫一眼是完全看不到小孩身影的，在图像上的位置，完全看不到。车上的三个后视镜也完全看不到，自然条件下根本没法发现小孩。车后一片空旷。<br />
<br />
2，倒车雷达。很多“不明真相的围观群众”都在自以为专家的态度以X6是好车，倒车雷达必须什么都能看到当理由，其实这些人有开过车也有根本没开过车的，但有一点可以肯定的是他们根本没有开过SUV以上车型及更高档车。倒车雷达并非你们说的倒车就一定会开着。雷达会有，但是障碍物的警示在生活里是很多的，所以很多人都会忽略这个问题，而且开车的老手在这个情况是根本不看倒车雷达的。至于成像雷达，这个是需要手动选择的，在你倒车时默认首先启动的是雷达界面，然后你可以选择成像。但这种后面空旷一片的情况下，三个后视镜其实完全足矣，100%没有人去开雷达图像。而且倒车雷达往往都是在停车入库，倒车塞车，车后有车，墙，隔离等情况下才会注意到它，而且还只有大部分新手才用。因此倒车雷达在这样后面空旷的情况下根本没人去关注，更不可能闲到去开成像。这不是停车入库，多此一举。也因此，小孩的弱小身躯和身高就明显成为了死角。别说是小孩了，在车后停一辆兰博基尼，你若不看倒车雷达，一样撞它。 也因此倒车雷达在这完全不起作用。<br />
<br />
3，察觉到车下有异物为什么没有停车? 那不是一般的普通轿车，而是X6，本身的减震能力绝非一般车型能够比拟，是根本就没有可比性。以小孩的身躯，对车内造成的颠簸效果还远不如一个马路牙子，而且底盘较高无法迫使车辆停下。以X6的车轮，底盘高度，动力性能，减震性能等综合起来，车的震颠不会十分明显，不过司机已经察觉，但一时间根本不会想到会是人。而这个时候，向前开一次过去。为什么这样做?换了我也会这样做。因为如果发现后轮轧过异物的话，而后视镜却见不到，那就说明物体在车下，不知道是什么，就怕是撞倒自行车过去，如果继续开，那就会伤到底盘，所以没司机会继续向后开，而是开出异物区域下车检查。这是为自己车的底盘考虑才做的动作。<br />
<br />
4，下车后为什么溜车。不要以为好车就不溜车，自动挡就不溜车。在低速行驶后踩刹车一般都不拉手刹，何况在这个情况临时停车，手刹会忽略。很有可能的就是在向前开的时候听到有人喊或是已经发现了车下是人，所以紧急停车下车检查。档位在N档很正常。一个正常的人在发觉事故发生后，没有手刹等下车查看的举动都符合正常人的逻辑。不注意的时候溜车，而非故意溜车。如果他真有主观动机去碾压，完全可以回到车上不用下来，轧完还能逃跑呢。所以很多人的主观片面想法都是不理智的。<br />
<br />
5，司机为什么要跑掉? 因为这个问题才造成了很多人故意杀人和故意逃逸的说法。我觉得这些人更是没有长脑子的。要说上面没开过车的人一个个自称专家出来耍聪明的话，那么现在这些要求司机及时救人的就是压根就没长脑子的。 理由很多，其一重要一点就是，孩子已无法挽救，那个情况根本救不了了。如果换成我，我也跑，我不会傻到在那里打120或是找家人。如果诸位看客觉得我说的话不对，不理智，或是不服，咱们可以换位思考。那么你来充当司机，我来当孩子的家属。不论你当时在那里做什么，是报警，还是打120，我保证，我上去要做的第一件事就是砍死，我绝对不留你活命。现在你们清醒了点了没?知道司机为什么要离开么?原因就在这里。他不会站在那里等到孩子家属到来，否则他只有死路一条，那个时候你是没法讲道理的，你怎么讲?你们会听吗?就这个问题我调查过我身边的很多朋友，如果他们是孩子家属，司机在那打120施救或是主动找你们，你们怎么做?我朋友的普遍想法都是第一时间砍死他！你们看见了么?那个情况下家属是无法理智的，所以必须要第一时间离开现场。而司机留下了车，并非驾车逃逸。那主观和客观上都可以说明司机没有逃逸的想法，他有自己想负责的想法，但是现在第一件事就是要保证自身安全。那么现在要做的就是必须要让警察找到你，或者你先找到警察才能保证自身安全。 在新闻结尾说警察是在小区附近将他控制的，所以很明显他并非想逃跑，而是要暂时保证自己的安全，必须让警察找到自己，他才能够获得安全。 如果真是主观杀人逃逸现场，他完全可以在车内碾压，之后开车扬长而去，这才是真正的逃逸。其实有很多交通事故发生之时，在发现出人命后，司机都会本能的做出反应，那就是先要保自身才能负责人。先逃出现场是最理智的行为，不要等到死者家属的到来抓住你，这是人之常情。把车留在现场，这说明司机想负责，但是暂时要回避，他要保命。如果司机是驾车逃逸，那就真的是逃逸。当然也有逃逸后自首，为的都是逃离不理智的亲属。 有谁能保证等到死者家属到来自己能活命回去的?<br />
<br />
综上所述，司机很镇定的离开，并且没有驾车逃逸，主观上他有着很强的负责态度，如果不想等到孩子家人上来砍死你，就要等到警察的到来。退一步说，撞的不论是孩子，还是老人，你父母，你亲属，你儿女，你丈夫老婆等，哪怕仅仅是撞伤，司机在那等到家属到来，都有被整死的可能，何况人已不在…… 我说到这，你们理智了么?如果你还一口咬定司机必须负责的留在那，那我可真要为你们汗颜了。希望你们今后驾车肇事都老实的留在那里等到对方亲属来和你心平气和的讲道理，而我还真想当一当你身边的那个围观群众。<br />
<br />
总而言之，孩子的家人有着不可推卸的监护责任，而司机疏忽大意酿成悲剧，这是谁都不愿意见到的。在这里，就是原本一个很平常可以完全按照正常方式解决的问题，却被一些无良的新闻媒体和失去理智的群众搞的及其复杂化，故意杀人的有，杀人逃逸的有，总之都充斥在喊打喊杀的浪潮之中。而新闻媒体竟然也没有理智，公正，客观的分析报道，而造成了所有人的舆论导向偏激，这不得不说是一个国家新闻媒体失职之耻，也是一国国民理智和思维及其简单之耻。多数人还拿着人道主义等幌子说话，难道把一个根本没有杀人想法只是出了意外的司机说成是杀人犯杀死他就是人道吗?大有一副不杀死他不罢休的架势，更有一种仇富心理在做乱。<br />
<br />
死者已安息，家人愤怒及痛苦，司机的痛苦，活着的人才最痛苦。 换了孩子家长谁都不能忍受，换了你们是司机呢? 责任是双方的，我不希望这种不理智和民愤能够扰乱司法公正，能够左右舆论导向。若真如此，那才是最大的悲哀。</div>

							
							
							
							
