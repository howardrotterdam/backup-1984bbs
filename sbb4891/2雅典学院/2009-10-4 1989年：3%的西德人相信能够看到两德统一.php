两德统一日回顾统一历程 <br />
 <br />
1990年10月3日，长期分裂的两个德国签署条约，正式实现统一。不过，西德，也就是联邦德国与东德-民主德国的统一进程之前就已开始了。但那时候人们还远远没有谈到签订统一协议的问题。两德最终能够实现统一，许多人都感到惊讶。 <br />
<br />
当美国总统里根1987年6月访问被高墙包围的西柏林时，德国已经分裂了近40年。里根总统呼吁说：<br />
<br />
&quot;戈尔巴乔夫先生，推倒这座墙吧！&quot;<br />
<br />
呼吁听起来令人鼓舞，但希望却很渺茫。尽管前东德的社会主义兄弟国家，尤其是苏联，当时已经长期致力于民主化和政治开放，但东德统一社会党主席昂纳克却拒绝戈尔巴乔夫的变革主张。1989年1月他威胁说：<br />
<br />
&quot;如果有继续存在的理由，那么，柏林墙在今后50年，今后100年都还会存在。&quot;<br />
<br />
在这一年里，只有3%的西德人相信自己在有生之年能够看到德国统一。但是，历史却像在变魔术。东德人的大规模出逃以及街上的示威游行让东德领导人受到越来越大的压力。11月初，东德领导人匆匆忙忙地出台了一个关于放宽旅行限制的条例。1989年11月9日晚，政治局委员沙波夫斯基过早地宣读了一份有关公民立即享有旅行自由的通告并且作出了错误的解释：<br />
<br />
&quot;因此，我们就一项规定作出了决定：每一个德意志民主共和国的公民都可以从德意志民主共和国的边界过境站离境。&quot;<br />
<br />
就在当天晚上，没有放一枪一炮，柏林墙旁边的东德边境检查站被汹涌的人流冲垮。而在那些天里，统一社会党内部也在上演着戏剧性的一幕幕。中央委员会成员君特・匡特，一个老共产党员，呼吁将统一社会党领导层中对民主德国崩溃负有责任的&quot;犯罪团伙&quot;处以死刑。他说：<br />
<br />
&quot;在国务院中我们取消了死刑。我赞成重新引入死刑，根据紧急状态法将那些给我们的党带来羞辱，让它在全世界面前蒙羞的所有人都枪毙。&quot;<br />
<br />
柏林墙的倒塌是那些在莱比锡，柏林，德累斯顿走上街头的人们意想不到的胜利。他们受到了波兰团结工会运动，匈牙利改革派以及戈尔巴乔夫的鼓舞。柏林墙的倒塌为德国统一铺平了道路。没有了柏林墙，整个东德便面临解体。那些希望建立一个独立，但是民主化国家的声音很快被统一的呼声淹没：<br />
<br />
“我们是一个民族，我们是一个民族... ...”<br />
<br />
在东德人民议院举行的首次自由选举中，东德基民盟领导下的德国联盟获得了胜利，该联盟赞成立即加入德意志联邦共和国。<br />
<br />
在西方，基民盟籍的联邦德国总理科尔早就看到了时代的机遇。他领导的政府在德国联邦议院各党派的支持下与新的东德政权以及取得二战胜利的盟国同时谈判，达成了实现两德统一的协定。科尔向欧洲邻国和全世界承诺：<br />
<br />
&quot;我们德国人已经从历史中吸取了教训。我们是一个热爱和平和自由的民族。我们绝不会将民主交给和平与自由的敌人。对我们来说，热爱祖国，热爱自由和睦邻友好的精神是相辅相成的。&quot;<br />
<br />
与当年二战的战胜国，也就是苏联，美国，英国，法国的协定对两德统一从国际法的角度作出了规定。德国在二战后首次获得了全部主权。前提条件之一是正式承认奥德河-尼斯河线为德波边界线。<br />
<br />
1990年夏，东德引入了德国马克并加入了经济与货币联盟。1990年8月，东德人民议会决定加入联邦德国，之后不久，双方签署了1100多页的统一协定。1990年10月3日，全德国的人都在庆祝德国统一。当时的德国总统魏茨泽克说了一段非常有哲理的话：<br />
<br />
&quot;人性化地实现统一太好了。这不是政府的契约，不是宪法，不是立法者的决定，而是取决于我们每个人的行为。&quot;<br />
<br />
1990年12月，统一总理科尔和他的基民盟/基社盟在1933年以来的首次全德议会选举中获胜。<br />
