粤语，英文为Cantonese，2008年正式被联合国定义为语言，并且认定为日常生活中主要运用的五种语言之一（Leading Languages in daily use） ，仅次于中国的官方语言普通话（Mandarin Chinese ）。换句话说，粤语（ Cantonese） 跟普通话 （Mandarin Chinese） 在同一个分类等级上，是平行关系，粤语不是普通话下属的一个dialect（方言）粤语跟普通话之间的关系犹如西班牙语跟葡萄牙语之间的关系，同是一种语系却不是同一种语言。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;联合国教科文组织的网页，把各个国家的语言都列出了，在“China”栏目内，排第一的是“Mandarin”（官话，就是普通话，也称为满清官语），排第二的就是“Cantonese”粤语了。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;事实上国际上也通常默认粤语是一种单独语言。.粤语被外国人定义为语言早在清末，外国传教士以及民国时期的外国学者,研究粤语后都认为粤语与汉语相差巨大,粤语是中国南方少数民族的语言,不是汉语,。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;粤语源于古越语和古汉语的混合，这大抵已是不争的事实了，西方学者普遍认为粤语是独立语言，中国学者普通认为它是中国的方言之一，中国人，甚至亚洲人，对于语言的概念，是不同于欧美人士的language一词的概念的，语言学家一般认为，若两种话语间不能直接通话，则这两种话语可定义为两种不同的语言；若两者间有或大或小的差别，但可以直接通话，则两者可定义为同一种语言的两种不同方言。根据这一分类标准，粤语跟普通话基本无法通话，应分别归类为两种不同的语言。但长期以来，国内官方一直把粤语定性为方言，其目的可能就在于防止广东因为语言的独特性而搞地方主义。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;每种语言都有其用法、发音上的特点，我们尊重国家有关推广使用普通话的政策和要求，但是语言往往不是孤立地存在的，语言是文化的载体，有其超越一般沟通功能的意义，所以推广普通话与保留、使用、保护我们广东人自己的母语――粤语并不矛盾。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;现实当中，在粤语的发源地，粤语的使用范围却有萎缩之势。日常教学被强制要求必须使用官方语言，要求教师用普通话教学代替母语教学，很多年轻一代说粤语的机会少了，甚至都不知道很多粤语通俗词汇为何意；而且由于越来越多的外来人口涌入广东，致使粤语的生存环境越来越狭窄，粤语在生活中被运用的机会少了。某些具有全国性影响力的场合，允许其他方言的出现，却始终未见粤语的身影，实在令人感到遗憾。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;对比国外，国际上所承认的粤语Cantonese,就是指广府话。这个“广府话”覆盖范围相当广，东南亚华人，欧美华人，使用粤语相当普遍，其中不乏粤籍学者和有地位有影响的人。说粤语的总人口，有可能超过一亿，从联合国的角度看，这已经是个大语种了。许多偷渡到美国的华人说：“要学英语，还不如先学广东话更有用”。外国人都知道有Mandarin和Cantonese之分。Cantonese早就已经列入了国际公认的语言的名单内，这不是什么人为的结果，而是历史形成的，其他语言想要进入这个行列，就不容易了。比如活动最积极的台湾人，至今国际仍未完全承认Taiwanese这种语言，更不要说是也想打入这个行列的上海话和福建话了。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;粤语在海外有好多人讲 ，影响力甚至比普通话大，粤语在全球华侨里面还是最通行的语言，甚至比普通话多人懂，这是由于早期海外华人很大一部分是从广东移民出去，东南亚某些国家入马来西亚所指的中文其实是指粤语。目前全球粤语使用人口大约有6700万，使用地区广泛。粤语不仅在海外华人社区中被广泛应用，而且支持着香港文化及南粤文化为中心的粤语文化，这使得粤语具有很强的影响力，可以说是目前世界上有较强生命力的语言之一而且粤语有一个国际化的名字――Cantonese。<br />
<br />
