													<h2>温家宝 当代袁世凯 中国叶利钦</h2>
						
						
																											<div id="postmessage_828072" class="t_msgfont">作者：傅尤杰<br />
政治就是赤裸裸的利益，任何政治行为都是利益追求的结果。从这个角度看温家宝谈政改，会得出一个什么样的结果呢？他为什么要冒着&quot;狼来了&quot;的风险，冒着给外界言而无信的危险，冒着损害自己已有民望的风险，三番五次地谈政改？他追求的利益是什么？<br />
<br />
<br />
　　很多人批评温家宝谈政改是在做秀，并冠之以影帝的称号。无庸讳言，截至今日，温家宝很多方面的确在做秀。他流泪，他下井，他蹙眉，他写&quot;多难兴邦&quot;，可以确定无疑的是，他都是在做秀。甚至包括他一开始谈政改，也有做秀的成份，意在打自己打扮成民主改革派的领袖。<br />
<br />
<br />
　　但温家宝是个绝顶聪明的人物，再做秀，也不会做到公然得罪胡锦涛及其他政治局常委的地步。他趁胡外出之际发文纪念胡耀邦，迫使胡不得不提前匆忙结束外访回国收拾残局；他突然造访深圳，给原定的深圳30周年搞突然袭击，并且大谈政改，迫使胡不得不推迟原定计划，以免让自己的谈话成为温的背书。所有这些，已经远远超出了做秀要达到的目的。唯一的解释，只能是：温家宝的确想推动政治改革。<br />
<br />
<br />
　　若论谈政改仅仅是给自己一个民主派的标签，温家宝早已达到了这个目的。他不用再说什么，已经有很多国人把他看成了党内的开明派，民主派。他是个聪明人，当然知道在获取好名声这一点上要适可而止，多说不宜，光说不做反而有害于自己的形象。他为何还要一而再再而三的，冒着损害自己名声的风险去谈政改，光说不做，不就等于把自己推动浪尖上，去替别人顶这个雷吗？唯一的解释只能是：他要把政改这个利器，牢牢地掌握在自己手里。<br />
<br />
<br />
　　因为只有政治改革，温家宝才能在有生之年，一跃成为中国的民主之父，成为中共内部的&quot;叶利钦&quot;式的人物，才有可能一跃而登中国的权力顶峰--试想，一人之下，万人之上的温家宝，还差什么，人生还有什么追求？不就是取胡锦涛而代之吗？<br />
<br />
<br />
　　而按照中共党内的规则，要么发动政变，要么通过改革，否则温家宝只能像其前任总理那样在十八大无奈退下。到时候，别说是影响力了，朱F基&quot;屁都不放一个&quot;，他温家宝还算什么？<br />
<br />
<br />
　　而当今天下，胡弱主之势已成。温汶川地震中收获巨大民望，胡温渐生嫌隙，胡温配也因汶川而变成温胡配，温在党内党外已经掌握了主动权。纪念胡耀邦的文章，不过是温抛出的一块试金石，而决不是像外界一些天真的知识分子所揣测的那样，是中共政治局常委的集体决定。知名评论人林和立就在《貌合神离，胡锦涛与温家宝正式分道扬镳》中认为，温撰文纪念胡耀邦，就是因为&quot;年初胡总要求温总把宏观调控大权交给第一副总理兼团派接班人李克强，但温断然拒绝并且凌厉反击&quot;，&quot;温总的官场自卫反击战的重点是树立自己作为党内最忠诚改革派的形象，不但继承了邓小平与前总书记胡耀邦的遗风，而且有魄力在二十一世纪重燃政治改革的烈火！之所以今年四月十五日胡耀邦死忌当天，温在《人民日报》发表文章高度评价胡的改革功勋，顺带回忆当年自己陪同胡考察贵州，获得此改革巨人的言传身教。但温总的文章对时任贵州书记的胡锦涛却一字不提！&quot;<br />
<br />
<br />
　　一些评论也认为，若是集体决定，要么胡锦涛写，要么党内理论班子写，怎么也轮不到温家宝。<br />
<br />
<br />
　　这个试金石，一试胡的胆量和反弹力度。二试党内的反应。三试社会的反应。<br />
<br />
<br />
　　温算是把胡这个人的胆小怕事看透了。据说，胡锦涛在美国得知温家宝趁他不在发表纪念胡耀邦的文章后，气得浑身发抖，立即结束外访，生怕权力生变。但胡也顶多是在身边人骂几句，回国内仍然装作无事的样子，拿温一点办法也没有。温知道，作为最亲民的总理，现在共产党需要他装门面，胡锦涛更需要他配合搞好权力布局，没有他的配合，胡根本驾驭不了局面，胡已经奈何他不得了。<br />
<br />
<br />
　　党内的反应，当然是积极的。那些既不是太子党，又不是团派的官员，看到温家宝如此强势，马上进一步向温靠拢。这个力量之巨大，谁都无法评估和忽视。至于社会的反应，这大家都看到了，天真的知识分子们心花怒放，只差对温山呼万岁了。<br />
<br />
<br />
　　有了这一层，温才索性一不管二不顾我行我素了。于是就出现了常委们去参加世博开幕式，而温家宝独自二上玉树灾区，令人瞠目的大路演。<br />
<br />
<br />
　　深圳谈改革 ，则已经不是试金石，而是号角了--不管你胡锦涛怎么谈，也不管你江泽民怎么唱，我温家宝要通过政改这面旗，要在十八大前大耍一通。<br />
<br />
<br />
　　要动就动真格的。温家宝已经不是病猫。以温家宝现在的民望，以温家宝现在的号召力，以温家宝早已练就的民主秀经验，比之中共那些在镜头前正经危坐生怕说错话的政治局委员，若搞竞选，谁选得过温家宝！更何况，面对太子党的飞扬跋赢，团派的治国无能，温还有党内干部绝大多数无门无派升迁困难的官心可用。若搞一场真正党内公开竞选，甚至推动一场全国的公开竞选，也只有温家宝最有可能当上最高权力第一人。这样，温家宝就可以告别行将退出政坛的命运，而继续主导中国的权力运作。<br />
<br />
<br />
　　诸位或有笑讽者，曰不懂政治。但请静下心来仔细想想，温家宝若不如此，作为有能力推动政改的人物，给外界只停留在言行上的骗子形象岂不是犯傻，岂不是自己推自己往火上推！唯其如此，反而要有的已经有了，剩下一个人生的最大追求，一搏之间，无论成败，都已成彪柄史册的历史人物，他有什么理由不干。<br />
<br />
<br />
　　当年袁世凯都下野了，在革命党武昌首义之后，借剿匪复出，仅为湖广总督身份，权力比现在的温家宝，不知小到哪里去了。但他接连迫退清庭内阁满族权贵，得到内阁总理之位，近而迫使清帝退位，当选民国大总统。那时，哪个懂政治的能在他下野时看到这一点？<br />
<br />
<br />
　　当年叶利钦以苏中排序第七的人物，就敢发动变革，公然与苏共决裂，挑战戈尔巴乔夫，不惜以自杀明知，&quot;八一九政变&quot;中，他不顾生命危险，跳上坦克，以一人之力挫败苏共保守派的反扑，终于赢得苏共跨台，而他登上权力巅峰。就在叶利钦跳上坦克之际，哪个懂政治的能看到那是未来的俄罗斯总统？<br />
<br />
<br />
