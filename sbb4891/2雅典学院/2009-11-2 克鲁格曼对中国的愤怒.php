													<h2>翻译的不是很准确，凑合着看吧</h2>
						
						
																											<div id="postmessage_310717" class="t_msgfont">The Chinese Disconnect <br />
By PAUL KRUGMAN<br />
Published: October 22, 2009 <br />
<br />
Senior monetary officials usually talk in code. So when Ben Bernanke, the Federal Reserve chairman, spoke recently about Asia, international imbalances and the financial crisis, he didn’t specifically criticize China’s outrageous currency policy.<br />
<strong>金融高官们说话从来都是云山雾罩，所以当美联储主席伯南克说起亚洲、国际间的不平衡和金融危机的时候他并没有明确的批评中国的无耻的货币政策。</strong><br />
<br />
But he didn’t have to: everyone got the subtext. China’s bad behavior is posing a growing threat to the rest of the world economy. The only question now is what the world ― and, in particular, the United States ― will do about it.<br />
<strong>他不用挑明了说大家也都知道他的潜台词。中国的恶劣行为已经对世界的其他经济体构成了越来越多的威胁。现在唯一的问题是世界，特别是美国，如果来应对中国的这种情况。</strong><br />
<br />
Some background: The value of China’s currency, unlike, say, the value of the British pound, isn’t determined by supply and demand. Instead, Chinese authorities enforced that target by buying or selling their currency in the foreign exchange market ― a policy made possible by restrictions on the ability of private investors to move their money either into or out of the country.<br />
<strong>一些背景知识：中国货币的价值并不像是英镑那样取决于供需关系的。相反，中国政府采用在外汇市场上买入或是卖出他们的货币的方式来强行控制人民币的价格---通过这种办法政府可以控制私人投资者的货币流向。</strong><br />
<br />
There’s nothing necessarily wrong with such a policy, especially in a still poor country whose financial system might all too easily be destabilized by volatile flows of hot money. In fact, the system served China well during the Asian financial crisis of the late 1990s. The crucial question, however, is whether the target value of the yuan is reasonable. <br />
<strong>当一个穷国特别是他的金融体系还经不起热钱的冲击的时候采取这种政策是无可厚非的。其实在1990年代亚洲金融危机期间这种政策对中国起到了相当好的作用。不管怎么样，最重要的问题是人民币目标值是否合理。</strong><br />
<br />
Until around 2001, you could argue that it was: China’s overall trade position wasn’t too far out of balance. From then onward, however, the policy of keeping the yuan-dollar rate fixed came to look increasingly bizarre. First of all, the dollar slid in value, especially against the euro, so that by keeping the yuan/dollar rate fixed, Chinese officials were, in effect, devaluing their currency against everyone else’s. Meanwhile, productivity in China’s export industries soared; combined with the de facto devaluation, this made Chinese goods extremely cheap on world markets.<br />
<strong>直到2001年，你还可以说中国的整体贸易水平并没有太多的失衡。但是从那以后，人民币与美元汇率保持固定的政策看起来就越来越怪异了。首先，美元在贬值，特别是对欧元，这种情况下保持人民币和美元的固定汇率实际上是让人民币相对所有的其他货币都在贬值。同时，与事实上的货币贬值相结合，中国的工业出口猛增，这让中国的商品在国际市场上特别便宜。</strong><br />
<br />
The result was a huge Chinese trade surplus. If supply and demand had been allowed to prevail, the value of China’s currency would have risen sharply. But Chinese authorities didn’t let it rise. They kept it down by selling vast quantities of the currency, acquiring in return an enormous hoard of foreign assets, mostly in dollars, currently worth about $2.1 trillion.<br />
<strong>结果造成了中国巨大的贸易顺差。如果允许人民币价值由市场供需决定，那么人民币的价格会猛增。但是中国政府却没有让人民币升值，他们持续的抛售人民币，换回了数额巨大的外国资产，大部分是美元，现在这些资产的价值大约是2.1万亿美元.</strong><br />
<br />
Many economists, myself included, believe that China’s asset-buying spree helped inflate the housing bubble, setting the stage for the global financial crisis. But China’s insistence on keeping the yuan/dollar rate fixed, even when the dollar declines, may be doing even more harm now.<br />
<strong>许多经济学家包括我在内都相信中国无节制的资产购买造成了房地产泡沫，为全球金融危机创造了条件。但是即使在美元贬值的情况下中国依然坚持人民币对美元的固定汇率，这可能会造成更大的危害。</strong><br />
<br />
Although there has been a lot of doomsaying about the falling dollar, that decline is actually both natural and desirable. America needs a weaker dollar to help reduce its trade deficit, and it’s getting that weaker dollar as nervous investors, who flocked into the presumed safety of U.S. debt at the peak of the crisis, have started putting their money to work elsewhere. <br />
<strong>尽管对于美元末日论有很多的说法，但是美元的贬值实际上是自然而然的并且是可取的。美国需要一个弱势的美元来减少他的贸易赤字，（这一段不会翻译）</strong><br />
<br />
But China has been keeping its currency pegged to the dollar ― which means that a country with a huge trade surplus and a rapidly recovering economy, a country whose currency should be rising in value, is in effect engineering a large devaluation instead.<br />
<strong>但是中国采取钉住美元的货币政策---这意味着一个本应该因为巨大的贸易顺差和快速的经济恢复而升值的货币，现在实际上却是在进行巨大的贬值。</strong><br />
<br />
And that’s a particularly bad thing to do at a time when the world economy remains deeply depressed due to inadequate overall demand. By pursuing a weak-currency policy, China is siphoning some of that inadequate demand away from other nations, which is hurting growth almost everywhere. The biggest victims, by the way, are probably workers in other poor countries. In normal times, I’d be among the first to reject claims that China is stealing other peoples’ jobs, but right now it’s the simple truth. <br />
<strong>在这样一个全球经济因为全面的需求低迷而依然不景气的时候，这更是一件很糟糕的事情。通过继续实行贬值的货币政策，中国正在吸走其他国家的本来就不旺盛的需求，这几乎伤害了所有地方的增长。顺带说一句，最大的受害者可能是其他穷国的工人们。在平时，我会第一个站起来反驳那种说中国偷走了其他国家的人的工作的说法，但是现在，这是个事实。</strong><br />
<br />
So what are we going to do?<br />
<strong>我们应该如何应对？</strong><br />
<br />
U.S. officials have been extremely cautious about confronting the China problem, to such an extent that last week the Treasury Department, while expressing “concerns,” certified in a required report to Congress that China is not ― repeat not ― manipulating its currency. They’re kidding, right?<br />
<strong>美国官员在面对中国问题的时候显得特别的谨慎，以至于在上周财政部一边在表达“忧虑”一边向国会提交了一份报告说中国并没有在操纵货币。他们真是搞笑，不是吗？</strong><br />
<br />
The thing is, right now this caution makes little sense. Suppose the Chinese were to do what Wall Street and Washington seem to fear and start selling some of their dollar hoard. Under current conditions, this would actually help the U.S. economy by making our exports more competitive. <br />
<strong>问题是现在这种担心是没什么意义的。假设中国做了华尔街和华盛顿担心的事情，开始出售美元资产。在目前的情况下，这实际上是在帮助美国的，使美国的出口更为有竞争力。</strong><br />
<br />
In fact, some countries, most notably Switzerland, have been trying to support their economies by selling their own currencies on the foreign exchange market. The United States, mainly for diplomatic reasons, can’t do this; but if the Chinese decide to do it on our behalf, we should send them a thank-you note.<br />
<strong>事实上，一些国家，特别是瑞士，已经开始尝试通过在外汇市场上出售本国货币来支撑他们的经济了。美国政府主要是因为外交原因不能这么做，但是如果中国决定替我们出售美元的话，我们应该谢谢他们才是。</strong><br />
