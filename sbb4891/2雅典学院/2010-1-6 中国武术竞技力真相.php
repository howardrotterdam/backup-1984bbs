其实不光是李小龙武功不行了，中国无论传统武术还是散打，在实战层面都是提不上筷子的，在现代搏击上中国也没什么地位，连派选手出国打K1都不敢，这个了解的人都有数，就不多说了。中国武术不行，但是中国那些武术迷却是天下无敌的，武术迷还分好几类，有些人同时归属数个门派。其中一种属于典型的精神分裂，在他们所幻想出来的那个中国里有很多深山老林，山顶上有个破庙，庙里有个老和尚，这个老和尚才是真正的武林高手。我问他，你去过什么狗屁深山老林，见过那个什么狗屁高手吗？他回答我没见过，但是肯定有，电视上都采访过呢！我……你丫看的不会是CCTV的《走近科学》吧？哲学派则喜欢说什么武术是强身健体，锻炼情操的，不屑于和人争斗，问题是这类人又非说中国武术是最厉害的，跟他们的哲学完全不符。还有一类无限制流说国外的比赛规则太多了，中国武术是实战用的，这些规则限制了中国武术的发挥。可是MMA比赛几乎没有规则，中国的高手们又为啥不去？于是武术迷又说了，谁说MMA没有规则！MMA不给插眼踢裆……这下我总算明白了，原来中国功夫流传了几千年，其实只有插眼和踢裆这两招才是精髓啊，太黄太暴力了……说到最后，武术迷怒了：你有种就留下电话，老子让你知道中国武术的厉害！咦，你不是强身健体，不屑争斗的吗，怎么又要打我，难道中国武术就是和强者比哲学，和弱者比拳头？其实他打赢我也不能证明中国武术很厉害，去打UFC吧，我真担心他有没有命活着回来。<br />
<br />
中国有很多人都属于讲话根本就不靠谱的，一点根据也没有。古代那会儿没有网络，科学水平也不发达，你说在遥远的什么地方有个武林高手会使什么影分身术还能骗骗人，现在还有人整天一张口就是民间高手、深山老林、少林高僧之类的东西，要我说就是逻辑能力差，科学素养差，智力水平低。这些人还时不时的展示几张少林寺僧人喉咙顶金枪、JB挂巨石的照片出来，想证明中国武术有多厉害，都21世纪了还有人把这种江湖卖艺人搞的胸口碎大石之类的把戏当成瑰宝不得不说是一个民族的悲哀，何况少林功夫都刀枪不入了，什么蒙古人、女真人、日本人都是怎么欺负中国的啊？除了武术以外，还有中医理论、气功之类的，也是同样的德行。这些东西都有个共同点，就是充满玄学式的神秘主义气质，你一句“科学无法解释”就把所有说不通的东西全说通了。其实道理都是一样的，四气五味、阴阳五行之类的东西，无非就是要让你无法查证，这样才好糊弄人。现在互联网上流传的那些骗人的帖子也有同样的特点，比如你一讲武术，除了讲深山老林的，很多人还会告诉你什么中国士兵在世界特种兵大赛上秒杀别国士兵，你一查，哪有什么特种兵大赛啊，于是对方又告诉你，这种比赛当然不可能公开啦，是我听我部队里一个朋友说的！你一听呆了，拿他没办法。还有那个中国缴获F117的帖子，作者是这么说：某次应海军某军官邀请参加宴会，那个军官喝醉后告诉我的！你又呆了，还是拿他没办法。有些则喜欢胡吹一堆牛B，说中国发明了什么武器可以轻易摧毁F22，然后这么说：点到为止，再说就要泄露国家机密啦！妈的全世界的机密都让你一个人知道了。<br />
<br />
那些龙迷就属于这类地摊小说看太多的，现在又出了个《李小龙传奇》，那更是不得了。现实中李小龙从来没跟罗礼士交过手，电视里两人却在全美空手道比赛上争夺冠军，李小龙一脚把罗礼士踢到浮空，在落地前连打十几拳，最后一个招牌式side kick把对手踢出十米远，还莫名其妙的吼了一句“我是中国人”，把编剧作为一个中国人的自卑之情完全流露了出来，却把龙迷们激动得不行。搞笑的是，所谓的龙迷往往对李小龙的真实生平一无所知，但人家就是觉得李小龙强。你要跟他们说李小龙不行，他们马上就暴跳如雷，但真要他们讲个所以然来又很为难他们。论点是有的：李小龙宇宙最强；论据要么没有，要么就是地摊小说里的内容；论证则是这样：连日本人都崇拜李小龙，你凭什么说他不强！当然更多是像疯狗一样咬人的。其实这种现象在中国非常普遍，最典型的是毛粉，据我观察，他们对那个时代的了解几乎全部来自历史教科书，你一问他大跃进怎么回事，前因经过结果一个也不知道，什么都不懂，但他大道理讲起来一套一套的，什么都敢说。老毛还是个争议人物，周恩来在中国就彻底的是个神了。我问那些周粉到底崇拜周恩来什么，他们说“周恩来清廉”。我就奇怪了，理论上来说，清廉不是对官员最基本的要求吗？这是什么猪狗心态啊？何况他到底清不清廉我还有保留意见呢，周的那点煽人事迹还不都是日人民报之类的喉舌宣传的，日人民报跟地摊小说有什么区别？就在这时，周粉又拿出一个帖子给我看：《周恩来！一个让联合国降半旗的人》……<br />
<br />
根据我在中国生活的经验，造神运动的成果从来都是豆腐渣工程。官方版的黄继光堵枪眼、飞夺泸定桥这些严重违反科学法则和人类常识的故事被一一戳穿之后，民间还要造出更多的神话来。说到关于武术的地摊小说，我见过最典型的当数《全面揭密美国黑市拳，排名第一的竟是中国人》。故事背景在纽约的黑市拳场，为什么是黑市拳而不是K1或UFC呢，就是要让你无法查证吗！K1每场比赛的结果都能查到的，那还怎么编吗。接着花大量的篇幅介绍黑市拳，讲了很多关于奖金、警方的打击之类的话题，介绍了黑市拳的技术特点，还采访了一些子虚乌有的拳手。讲这么多和中心思想无关的内容，目的只有一个――让你相信这篇文章是真的。OK，主线层层推进，下一段的主题是黑市拳手远比正规拳手厉害，你一听“黑市”两个字，觉得恩好象应该是这么回事。这样作者终于可以推出这篇地摊小说最大的谎言了：黑市拳最厉害的是中国人，名叫唐龙！黑市拳的水平比K1高，最厉害的又是中国人，这不就证明中国武术最牛B了吗？当然作者没有得意忘形，接着他还得继续修缮这个谎言，首先告诉大家唐龙在最后一场比赛输了，为什么呢，因为他虽然是最强的，但还是有个小弱点被对手抓住了――这样人物才显得真实。最后再来个黑市拳拳手排行榜，别的国家也得分点名额，不然就显得太假了，至此，一篇毒害了无数人的地摊小说诞生了。现在你在网上一谈到武术，除了李小龙、规则、民间宗师、特种兵、一招杀敌、少林高僧这些关键词以外，几乎还必然会温习一遍中国黑市拳王唐龙的传说。<br />
<br />
所以我就说有些人逻辑能力差科学素养差智力水平低，这种屁话他也信。现在都他妈2008年了我管你丫的黑市拳白市拳世界最强的男人会连个youtube视频都没有吗？特别是里面还说中国人能一脚踢倒27英寸的铁柱，我靠27英寸是几厘米啊？都谭望嵩啊？<br />
<br />
前几年我看到一个帖是一个养狗人谈自己养的近十条斗犬，其中提到藏獒在斗犬中其实是排不上号的，这下搅了马蜂窝，马上无数块砖头招呼了上来。其实我本人对斗犬并不了解，藏獒的实力究竟如何我也不敢说，就是看回帖特别搞笑，那些骂楼主的不仅没一个人亲眼见过藏獒，甚至连藏獒长得啥样都不知道，但说起话来个个暴躁无比而且理直气壮，我就不明白他们到底在生气些什么，难道一条狗也能代表中国的对外形象了？这是什么世道啊，人可以变成神，连狗也可以变成神了。而他们反驳的说辞大多是这样：你看到的那些藏獒根本不是纯种的，真正纯种的藏獒只有西藏才有（深山老林之西藏版）；藏獒适合在高原作战，你有种叫其它狗上西藏跟藏獒打（规则限制之高原版）；我曾听一个住在西藏的人说过，他们那里一只藏獒咬死了七只狼（27英寸铁柱之动物世界版）。这么多獒迷群起攻之居然都讲不出一句人话来，直接戳穿了藏獒神话的真相。<br />
<br />
倒是那个养狗人说，藏獒对自己忠心得有点过了头，他跟家人吵架时藏獒居然想冲上去咬他的家人，导致他们一家人都不敢对他大声说话，而且藏獒根本没法和别的狗和平相处，只要看到四条腿的，不管对方比自己大多少，都想冲上去咬一咬，主动挑衅的结果却又往往是铩羽而归。这几句描述一下让我想到了中国的某个特殊物种，而且你仔细一想，这条狗为啥会这样，不都是因为智商低吗？难怪他娘的快要绝种了……</div>

							
							
							
							
