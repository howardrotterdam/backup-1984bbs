<br />
-------------------------------------<br />
回豆瓣读书小组的一个朋友，他谈到写武侠小说如何突破。<br />
冲动回了这篇，原创-----<br />
<br />
突破武侠小说的写法，我近来有想法，最近常去健身房，认识一些搏击爱好者和搏击教练。常聊到武术，且近几个月都在泡一个非常专业的武术论坛（说的就是山上了），了解到太多关于武术真实层面东西。如果说以往武术在脑海是一种概念，那现在被击破的概念估计成千上万；如果说偶像已经倒塌，漫地躺下的都会是大师的尸体，如果说历史等于大话，《大话西游》就是外交正史了。 <br />
　　一些发现如下： <br />
　　中国现在比较盛行的武术中，真正职业化，可以上擂台的只有散打。散打吸收了中国交的摔法，拳击和泰拳等特点，使得武术有机会登大雅之台，有机会和世界搏击上同样享有盛名的巴西柔术、泰拳、俄罗斯桑搏进行不同类型对抗；也有机会到日本的K1台上参加比赛。只是成绩非常不理想。世界散打锦标赛也举办了好几届了，影响力一般，冲着对中国武术的崇拜，依然有无数的国际友人痴信和痴迷散打。然都不成气候，真正大型的比如UFC等高手汇集赛事，基本无散打选手敢上台（包括一切国术）。国内组织了很多中美中泰中越中欧中韩中日等等赛事，基本上都是我们一流选手和他人三流选手的对抗。现在发展稍好点，敢打一点真正的比赛了。 <br />
　　散打是中国武术目前最适用的比武类搏击。一个专业运动员（甚至只是体院的散打学生）通常不到20秒KO掉名动四方、家不外传的民间武林高手。这样的事情几乎在全国时有发生。但传统大师一般都以比赛不能插眼睛和踢挡为借口保护颜面和饭碗。不过即使是散打这样搏击性非常强的运动，在一般的对抗中，就是搏击爱好者经常说的街斗中，同体重的散打运动员很难打过柔道运动员，更打不过古典摔交专业运动员，和举重运动员对抗都没有十足把握；因为他们的摔法在水泥地这种环境往往一摔致命，比任何的拳脚都厉害十倍。同时他们的抗击打能力和力量非常强。 <br />
　　中国的拳种特别多，即使现在科学来看，分的也是内功和外功之分。现在保留的拳种中多以为明清为主，越靠近现代的拳种实战能力越强。如八极拳，听闻中国的特种兵的致敌术就来自该拳种。中国拳大部分的打法和攻击法从理论上非常科学，但犯了最大的错误，就是拳讲究从腰部位发出，露出脑袋这个大空门，认为这样才能发出腰劲。而西方拳击同样追求的是拳要发出腰力，但拳从嘴部发出，带动全身力量。这样使得西方拳击的灵活性提升无数倍，中国种种的拳术在西洋拳击的简单组合拳下往往走不了几个回合。中外搏击的差距还在于，国外搏击术非常注重速度/力量/体能的训练。在攻击手段上尽量简单化，国术号称轻盈巧灵，其实在专业运动员眼中看来又慢又无力，还三两下就踹气不来了。 <br />
　　中国的气功其实是存在的，且入门槛很低，一般有点基础和不错老师教的，2个月左右就能摸到气息，半年一年的就能运气。那些卖外功的，就是气功练的不错，可以把气汇集一起。气功的运转需要时间，所以这个功夫基本上只能用来做表演，在擂台和实战中毫无用处。中国著名的铁纱掌/鹰爪等也都是存在的，现代人要练也不难，无非几年时间，但必须要有老师盯住，因为要和使用药物一起进行，没有师父指点往往会变成残废。那些传统的名掌，所谓一招致命的，都是配合气功和毒药一起来练的，每天浸泡和打击，最后把人的手变得对毒药免疫，在实战，特别是比武的繁文缛节的礼仪后，也有可能在群殴的浑战中，一招致命，所谓一招致命就是缠打中发掌或爪深入到对方皮肤，令其中毒死亡。所以五毒掌等能名扬天下。 <br />
　　但这么说是不是说中国武术不行呢？不是，只是和中医一样还没走进科学和系统的范畴中。中国人在几千年的历史里，经历多少战乱兵变，匪乱和强寇，民间的势力为各方稳固都在努力创造自己的致敌之术，说是所谓的花拳秀腿，那也真是愧对古人。中国武术的颓势还真是从所谓的新中国成立后开始，先从套路入手，废除了武术中所谓不雅，攻击凶狠的招数（比如有门派就专练攻击档部，有专练习挖眼睛的）。中国武术中最优秀的攻击，冷兵器技术也基本被禁。武术变成了舞术。民间高手的那些秘术，一招定命运的东西也没有舞台（这个一招不是夸张的把人击飞和其他悬乎），电影和小说更不负责把武术的高低判断在技术的奥秘上，人都是两只手，两条腿，哪里能有那么多奥秘呢，其实中国武术的奥秘在一些创意上，在攻击手段上非常隐蔽，在攻击训练中非常神秘，在攻击过程里其实是非常难看和非常难堪，而真正的攻击结果都是难以让人接受。因此传统中国武术讲究武德，受徒也严格，因为每天练的是杀人之术。 <br />
　　金庸的武侠小说太浪漫，太不社会。古龙的更是。武侠小说想要真正焕发生命力，不应该是从更高更强更大上发展，而是多些社会状况，回到古代社会中去。打理古人的油米柴盐，关心他们的衣食住行。要象《水浒传》，象金庸先生的《连城诀》，还要有点粱羽生的迂腐（不过他写的人一打斗就是几个小时太假了），在武功上不要迷信秘籍，特别是古秘籍。其实越是老的搏击越无用，要不后人改他做甚。在对武功的练习上，都应该是走类似《醉拳》（成龙演的第一部）的风格，写实。扎扎实实的练身体素质，苦心孤诣的学一招鲜；古人搏杀中最重要的冷兵器的使用应该重点突出，十八般武器都该有，特别是长武器，还有奇门暗器，都该还原历史。我一直渴望看到有人写的是真实的江湖，使用的是那个时代真实的武器。如有万人敌人者，最厉害也不过《卧虎藏龙》中的玉娇龙，客栈中那段打斗尤得我心（狄仁杰3中也有一段惊艳的客帐）。如果写作者文学造诣不错，怀着神秘的绪头，那也该写类似老舍先生的《断魂枪》，浪漫而不夸张；或者干脆意识流点，写余华《鲜血梅花》这样的小说，现实却如一段入醉入迷的故事。<br />
　　<br />
　　--------------------------------------------- <br />
　　写这么多的武术实际状况，其实是想说任何社会都是残酷的，侠出现为了缓解残酷，同时制造了更多残酷。我真希望能看到的小说是腥风血雨的江湖，侠是为少量的理想者在执行，决不是什么时髦职业，至于具体的攻击之术，本来就草莽英雄的谋生本能，没有那么多虚文矫饰；大历史的面孔如此真实，武侠也应该如此。 <br />
　　</div>

							
							
							
							
