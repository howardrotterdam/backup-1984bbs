													<h2>中国武术竞技力真相</h2>
						
						
																											<div id="postmessage_416379" class="t_msgfont"><a href="http://focus.news.163.com/10/0103/10/5S3M5FN200011SM9.html" target="_blank">http://focus.news.163.com/10/0103/10/5S3M5FN200011SM9.html</a><br />
<br />
传统武术在攻防技术中掺杂了大量象征性动作和门派仪式动作，从功法上看，传统武术仍保留着很原始、低效的操习，与其说是增长格斗实力，倒不如说是在祈祷、苦行和磨炼耐心<br />
<br />
梅惠志第一次真正接触泰拳是在1990年。当时，作为北京武术散打队的首任总教练，他带队参加刚刚设立的京港搏击会，虽然带去的都是当时的国家级顶级散打高手，但那一次中国散打惨败于泰拳。<br />
<br />
“没办法，我一看到泰拳用肘用膝，就只好丢毛巾。”梅惠志说，当时，北京武术散打队第一次出国，根本不懂什么是自由搏击，对泰拳的膝肘攻击很不适应。“与泰拳硬碰硬，我们根本打不过，拳脚打在对手身上，跟打在皮球上一样。而当时我们的队员抗击打能力比较差，挨上两三下膝肘就不行了。”<br />
<br />
此次惨败之后，梅惠志等人才把泰拳的录像拿回来反复研究。“泰拳非常硬朗，主要使用边腿和膝肘。他们的记分主要是以击倒而定的，所以踢你时一下是一下，你的重拳他们根本就不躲。但我们的散手没人家能挨打，散打只有发挥摔法的优势才有取胜的机会。”梅惠志说。<br />
<br />
不久前泰拳拳王放言要秒杀中国功夫，无论是否炒作，让人们再次审视声名在外而又难以捉摸的中国功夫，如果站上擂台，竞技力究竟几何？<br />
<br />
散打为何难敌泰拳<br />
<br />
梅惠志是北京市散打运动的创办者。上世纪70年代末80年代初，作为北京什刹海业余体校国际式摔跤教练，他在北京武术队主教练吴彬和中国式摔跤教练李宝如的协助下，开始练习散打。<br />
<br />
练习散打的原因是当时中国武术套路表演走向了世界，并获得国际好评，影响力越来越大。许多外国的武术爱好者来到中国，都想与“中国功夫”较量较量。尤其是李连杰主演的电影《少林寺》上映之后，中国功夫实战能力如何，成为一个亟待证明的焦点。<br />
<br />
“那个时候来挑战的国外武术爱好者，很多都由我来对付。”梅惠志说。但来较量的一般都不是职业搏击手，由摔跤转为散打的职业运动员梅惠志完全能够应付得来。“在1990年第一次带队参加京港搏击会之前，我们对世界上的整体搏击状态并不了解。”<br />
<br />
其实，中国功夫与泰拳的较量一直在进行着。目前可以查到的资料显示，从1921年开始，中国功夫就在向泰拳发起攻击。但除了1922年，由流亡泰国，本有武功，并拜华裔泰拳宗师为师的李德与泰拳手打平之外，其余皆遭惨败。<br />
<br />
而1958年至上世纪80年代，由香港和台湾组织的数次中国功夫与泰拳的比赛，也仅有一场平局，其余都告失败，而且败得相当惨，最短的一局仅坚持了20秒。<br />
<br />
但近几年，散打所代表的中国功夫在与泰拳的对抗中，却出现了赢多输少的局面。“双方研究规则，泰拳可以用肘膝，我们可以用摔法，做好针对性练习，赢面比较大。”梅惠志说。<br />
<br />
不过，近几年的中泰对抗赛，中国散打的成绩受到了不少武术爱好者的质疑。人们在有限的中泰对抗录像中，以及各种中泰对抗赛中国散打大获全胜的消息中，对泰拳手的来历及资质并没有多少了解。<br />
<br />
相对来讲，为众多搏击爱好者所熟知的泰拳王播求与中国散打冠军孙涛的对抗，更像一次上规格的对决。在这次比赛中，播求很顺利地拿下了孙涛。这个结局似乎并不是那么令人难以接受---从双方的简历可以看出，作为职业泰拳手，播求在日本最知名的站立综合搏击赛事K-1上风靡全球，其成绩是170 战，155胜；而作为中国体制内的运动员，孙涛的比赛次数只有24战。<br />
<br />
民间并无武功高手<br />
<br />
虽然，从中国功夫与泰拳的对抗历史中，中国传统武术的成绩还不如散打来的好看，但大多数中国人仍然相信，真正的中国武术的技击精华是在民间，在传统拳术中。虽然没有任何确实的证据证明这一点，但人们更愿意相信，在那些与世隔绝的密境，有神仙般的武林高手存在。<br />
<br />
“虽然存在民间有高手这一说法，但民间拳手的水平并不高。与散打相比，基本没有对抗性。”梅惠志说，他曾经会过许多民间高手，“很多都坚持不到十秒八秒。”<br />
<br />
而在1980年和1981年，北京搞过散手试点，当时来自民间的参赛拳手有上百人，包含了八卦、太极、大成等等拳种。“但比赛刚开始没两天，一看进入半决赛的选手，都是练习散打的了。”梅惠志说。<br />
<br />
民间武术大多没有经过对抗训练，一上擂台就“不管练习什么拳，最后都成了王八拳”。对抗起来根本没有反应，挨上两下就不打了。<br />
<br />
“有一位练习八卦掌的，比赛开始了，他还在那转圈子，被我们的队员追上去，踢了两脚，就不打了。”梅惠志说。那一次，最后冠军都被体校队员拿下。<br />
<br />
1987年，梅惠志带队参加武当山全国武术擂台赛，这一次的场面比北京的散手试点更加热闹，赛场上有扮成武松模样的，还有和尚、老道&#8943;&#8943;比赛前表演，架势挺吓人。有人一掌把木板中的铁钉子拍了出来，可一上台打擂，那人只挨了一脚，自己就跳下擂台了。<br />
<br />
还有一位神秘人物，自己爬上擂台要求比赛，当地组织者要求他先报名，但遭到拒绝，理由是“不敢留名，打完了再说”，并自称已经“毫无欲念，不吃荤腥”。看到这种情形，梅惠志专门交代队员不要踢第二脚，因为第一脚把人踢倒，第二脚必然会踢头，这样会导致这些没有任何对抗训练的对手直接休克。<br />
<br />
对于民间有没有高手，著名武术家赵道新早在上世纪80年代就指出一个常识：“在那些与世隔绝的不毛之地，消息闭塞，交流不便，物质贫乏，隐士们如何能启发悟性，拓展眼界，避免徒劳创作呢?又怎样能通过大量‘见手’来交流技术，衡量自己？否则，又是怎么知道他们技高一筹，掌握精髓呢？生活问题怎样解决，营养哪里补给，资金、器具谁来提供？如果自食其力，花大量精力安排衣食住行，训练效果怎能提高？”<br />
<br />
而在梅惠志看来，传统武术主要是训练方法和意识的落后，讲究的是口传心授，多是说招说手，平时几无实战训练。“举个很简单的例子，对方边腿踢你，散手队员会一手格挡，一手反击。传统武术可不这样，他要先做一个云手，动作好看，但对方早就踢到你了。我们同他们交流时，分出胜负也就一个照面，用的就是一个简单的迎击。”<br />
<br />
传统武术缺乏对抗训练导致了许多悲剧。1987年，在一次两省警察的集训中，某省一名练习传统武术的警察与另一省份练习散打的警察对练，结果因为前者从未做过对抗训练，在被摔起的时候没有任何防护意识与技巧，头部直接坠地，导致死亡。<br />
<br />
中国武术极度缺乏技击性<br />
<br />
“但传统武术并不是这个样子的，在很久很久以前，传统武术也是一拳一脚。”中国武术院社会组副主任刘普雷说。<br />
<br />
作为格斗技术的武术本来就是打出来的，不是说出来的。除了在影视剧中，我们很少看到中国武术与外界的对决，那么中国武术的技击性到底如何？<br />
<br />
民间武术家赵道新认为，中国武术最大的骗局就是具有所谓的“技击性”。虽然传统武术有些技法还是包含着较高的技击性和潜在技击性，但赵道新肯定，当今中国武术在整体上极度缺乏技击性。以全球格斗界的战略眼光看，可以说已丧失了技击的竞争能力。<br />
<br />
在赵道新看来，今天的传统拳术与学院武术一样以套路为主，并混入了冒充古拳法的套路新作品。而套路与篮球、游泳、登山一样只是提高运动素质的锻炼方式，却不针对格斗需要，特意发展那些直接专用于格斗的素质和技术，从根本上说称不上是技击训练。<br />
<br />
从打法上看，传统武术在攻防技术中掺杂了大量象征性动作和门派仪式动作。这些动作与技击无关。从功法上看，传统武术仍保留着很原始、低效的操习，与其说是增长格斗实力，倒不如说是在祈祷、苦行和磨炼耐心。<br />
<br />
实际上，从民国时期以来，人们对于传统武术的套路对招一直在进行着理想主义的尝试。“2003年我还亲自参与过这种尝试。”刘普雷说。当时，散打王节目在湖南卫视火爆上演，四川的几位武术爱好者找到刘普雷，希望能够录制一些对招的节目。<br />
<br />
“在他们的想象中，仍然还是那种你一招白鹤亮翅，我一招黑虎掏心。”刘普雷说，早在70年代末80年代初练习散打之前，他就参与过这样的试验，但从来没有成功过。“你想啊，就像治病一样，怎么可能是开好了方子等病人呢。”<br />
<br />
但四川的那几位武术爱好者很固执，找到了一位喜好武术的企业家赞助，还找了四川省套路队帮忙，按照这种套路对打的方式进行，结果可想而知。“拿不出手！”赞助该项目的企业家对刘普雷说。<br />
<br />
相反，因为那些练习套路的孩子并没有经过技击训练，反而很容易受伤。“尤其是小关节之类的，动不动就会受伤。”刘普雷说。而这相对于现代搏击来说，无疑是没有任何技击性的。<br />
<br />
但刘普雷并不认为中国武术的竞技能力在国际上就毫无竞争力。他更加相信是规则原因，对竞技结果有直接影响。<br />
<br />
梅惠志也是同样的观点。“如果双方约定在相同的规则下进行三个月的训练，然后再进行对抗的话，胜负难料，反正是有得一打。”<br />
<br />
而在本刊记者的采访过程中，对中国功夫竞技水平最悲观的说法来自一位在国外学习柔术、到中国创办竞技俱乐部的华人。他认为以当前中国武术的竞技水平，需要三年的时间才能达到日美水平。<br />
<br />
国际拳坛为何不见中国选手<br />
<br />
既然中国武术的竞技水平在国际上并不落下风，为什么在诸如UFC、PIRDE、F―1等国际顶级的搏击赛事上，见不到中国选手的影子？<br />
<br />
“这是因为我们采用的是全运会体制，我们的精力没放在外出竞技上面，而要打F―1必须要长时间的竞技训练。”刘普雷说。<br />
<br />
1998年年底，刘普雷曾经带队去过K―1现场，当时主要是带套路队去做串场表演，其中还带了两位散打运动员做现场表演。“那是我第一次去K―1现场，到了那里才知道原来一个擂台赛能做成那个样子。”刘普雷回忆说，六万多人的现场，灯火辉煌，壮观极了。<br />
<br />
接触过K―1之后的中国散打选手曾经低调参加过这个比赛，但结果并不理想。不过，本刊记者采访中发现，实际上中国的散打运动员都比较想参加诸如K―1的国际顶级格斗赛事，一方面是为了证明自己的实力，另一方面是为了高额的奖金。<br />
<br />
但是，作为国家运动员的中国散打高手并不自由。首先从身份与资质上来讲，专业散打队是行政性的，全运会的体制需要这些运动员全力以赴打好国内的比赛；而在技术实力上，我们的打点打分的比赛体制，并不适应F―1。参加F―1的拳手都是为了高额奖金，“人家是挣钱来的，打起来很拼命。”刘普雷说。<br />
<br />
“其实，最大的问题是教练。”一位不愿透露姓名的搏击手说。国内的全运会体制使得职业教练以及运动员都必须围着全运会使劲，对于他们来讲，根本没有去国外顶级搏击赛事交流的必要。而更重要的是“他们输不起”，这直接关系到他们的根本利益。<br />
<br />
现代性质武术俱乐部体制在中国的发展尚处于艰难起步状态，要培养出自己的职业搏击手，还需要一段不短的路程。但好在中国武术协会对于俱乐部的建设越来越重视。“这是我们的一个方向。”刘普雷说。<br />
<br />
俱乐部的建设不仅能够极大地活跃中国的搏击市场，还能够拓展职业运动员的就业。或许在不久的将来，我们将不再感叹“国外的搏击手都是明星，国内的搏击手都是保安”的尴尬现状。■<br />
(本文来源：t望东方周刊 作者：王开) 胡彦</div>

							
							
							
							
