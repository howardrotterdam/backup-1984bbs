																											<div id="postmessage_416380" class="t_msgfont">土摩托日记<br />
偏见源于无知<br />
原来不止是中医能引发网络混战啊<br />
2010年一月4日16点25分 | 分类: 转帖 | 由 土摩托 | No Comments &#187;<br />
Tags: 中国武术<br />
<br />
t望东方周刊登了篇文章，《中国武术竞技力真相》，我一看留言，果然又吵起来了，好玩死了。选几个贴在下面，以供大家治疗抑郁症：<br />
<br />
那只能说明你们没见过真正的高手!一群井底之蛙而已！我在上高二的时候，当时学校开新年晚会，高二-四班的体委上台表演武术。当时谁都没当回事，因为他平时很低调很老实，不多言不多语，从不打架，不象多数体委那样张扬、嚣张。可是表演之后，所以人都傻了，用手指钻砖头，用手指劈断拳头粗细的鹅卵石，用手指劈断砖头，大家注意了，是手指而不是手掌！楼上各位，如果这不是真功夫，你们愿意拿你的脑壳做实验吗？试一试是不是假的？你们有愿意的吗？后来才知道，他8、9岁的时候就被一个老道给带上山学艺的。这是1988年的事，真正的武学高手，都隐于市井，不为这些名利所动。所以，比赛也好，擂台也好，很难看见真正的高手的！如果没有高手，zhong、nan、hai里的保镖都是哪来的？都是散打出身吗？只能说明楼上的一群SB罢了！<br />
<br />
而那次对敌老太的人，随便拿出一个就是十个柳惠志也根本不能近前。那十几个人是一起上的，结果，老太原地转了一圈，这十几个人都被甩出去几丈开外。此是因为，老太每天清晨都来练功，总是在公园一偶练太极， 而那些练散打的也是想博个彩头，隐约听说过老太身手不凡，有一次其中一个人抱着试试看的样子，出了7分力道，一记左重拳当头劈下（他想就算打坏了，他赔钱给老太家人不就行了。）哪知结果相反，老太连躲都没有躲，单手粘住对方进攻的左手，向自己方向稍微带了一下，紧接着一个反手回送，只见那人，顷刻间已经摔出几丈开外。这次十几个人依然是抱着好奇的心态一试，但依然是被摔出几丈开外，从此，莫不能服。<br />
<br />
各位去百度上搜一下“吕紫剑”这个名字。117岁，敢说中国武术出不了高手？<br />
很怀疑作者到底是否真正懂中国武术？套路和实战完全两回事！知道什么叫自古八极不上擂不？因为一比武就会分生死！很多实战型的中国武术都是这样！真正实战型的武术都只杀敌不表演。为什么只杀敌不表演？因为真正的武者都有一个武者的自尊，他们不可能容忍自己如同戏子一般被人当看猴戏一般观赏！<br />
估计大多数人都以为太极拳就是公园里老头老太太天天玩的那个吧？实战型的太极见过没？？<br />
说中国武术不行的人也不动动脑子，中国几千年的冷兵器时代，各种武术不断经过实践与优化，怎么会那么不堪？笑话。<br />
<br />
写这篇文章的人恐怕就根本不懂武术，散打就是垃圾，只有动作，没有气。而传统武术注重的是气。你就看一看一般练过武术的人能活多长就行，很多都比平常人能多活十几年，练了泰拳能活多久。泰拳最大的一个弱点就是只有攻，没有防，而且不会用腿，通过步伐的运动来攻防。如果学武术的人都改学散打了，中国武术就完了。<br />
<br />
这纯是一篇混混文章。文章中那个姓梅的，大家看看他原本是干什么的，后来才搞散打。这样半路出家的人本事能大到哪里去。他有什么说服力？<br />
我这里先说说我知道的中国武术。人们经常说什么“中国武术不注重实战，都是花架子。”我知道的家乡民间武士是怎么教授徒弟的呢，徒弟要出师，师傅会让他去一个地方，徒弟事先不知道师傅叫他去干吗。经常是徒弟一推门进屋，师傅就是迎面一巴掌或者其他攻击。如果徒弟应对不了，他就出不了师。为什么会有上面那说法呢？我个人认为这与当年三大内家拳在北京流行有关，以太极为例子，最流行的杨太极本身是脱胎于陈太极，那么杨太极是怎么流行起来的呢，他是把陈太极中的跳跃等动作全部取消，当然也有所创新。为什么要取消那些动作呢？因为那些动作很累人，那些八旗贵族子弟，风骚墨客文人怎么吃的消呢。就因为这种流行，中国武术就出了一大批嘴比拳大非常有文化的高手，于是中国武术就要注重养生，注重武德，注重草泥马，就是不注重实战了，文章中说的那种没有抗打击能力的民间高手，多半就是这种人，有些地方练武术的人旧时候叫做“操扁挂的”，操是操练的意思，不要乱理解，扁挂是什么呢，就是半米多长，两寸多宽的铁条，有时候铁条的前面还有小铁环，这东西干什么用的呢？往自己身上打的，这样修炼出来的身体绝对比那些踢芭蕉树的身体强度高。<br />
再到了后来，不知道什么原因，如果你们看到了70年代或者80年代的体育教材中关于武术的一些内容，比如出掌要求手掌与小臂要成90度角，这美观大方的一掌要是重重推在别人身上，多半是自己手腕骨折。从此武术就成了舞术。<br />
至于民间还有没有高手，我肯定还是有的。只不过有修养的高人通常都会牢记历史，不轻易出来蹦达的，也不会走上擂台自掉身价给别人当猴看的。肯出来蹦达的，多半就是那种半罐水，比如上台跑圈圈的，他肯定不知道八卦掌跑那圈圈有什么用，就是傻跑，呵呵，这里也就不解释了。我曾经见过一个民间人士，可以在大约一米开外凭空打破一个大点西瓜大小的陶罐，我想不出现在在竞技台上的那些人谁能防御这一招。我想见过这类人士的人在全中国应该不少吧，所以才有说法是“武术在民间”。<br />
<br />
中泰比赛刚刚结束 中国功夫大胜泰国 这是有目共睹的 为什么还有人发这样香臭不分的文章呢 现在国内总是有些逆向种族主义者散布一些懦弱白痴的言论 这些人完全是神经病 根本不了解中国功夫 不了解中医 就象肚子里的蛔虫一样 把自残当成是一种爱好 自虐得到了智障的程度难道这些傻瓜不知道现代技击术的奠基人就是中国人李小龙吗 西方技击术虽然有独到之处 但是其技术性是片面的 技击哲学远不如中国的高明智慧空手道像机器人一样 巴西柔术形态丑陋 泰拳技术比较单一 等等 这些技击只能称一门至尊 远不如中国功夫那样全面 高超之所以开放初期不敌国外是因为社会政治的原因 不是中国功夫不行 现在看起来 功夫的进步很快 中国哲学的运用在实战当中逐渐发挥出它的优越性那些自残的傻瓜快闭嘴吧 不要拿亵渎中国文化当饭吃了。<br />
<br />
