<br />
　　中国台湾网9月28日新竹消息 孔子第73代嫡系玄孙、北京大学中文系教授孔庆东，昨日（27日）在台湾新竹举行的《中华文化大讲堂》上，对两岸万余师生发表以《中和致美・礼运中华》为题的演讲。孔庆东还在演讲会场与孔子第78代玄孙、台湾清华大学电机系孔维暄相见，二人都是孔子本家，此番跨海相遇，所有与会嘉宾有幸一同见证这一场“中华血缘的神奇之旅”。 <br />
<br />
　　孔庆东在演讲中说，孔子中和之美的中心思想就是“和”。我们不仅要求现在的学生们知道孔子的话，而且要通过某种仪式让它再一次进入我们的心灵。 <br />
<br />
　　孔庆东指出，两岸师生祭孔大典就是礼的形式之一，用这样的方式使圣贤之言不仅仅停留在语言的层面，还能提升我们的精神境界。自我的修养提高了，才能深刻理解他人，理解世界，才能够知礼，才能够以礼谋和。正因为我们中华民族的人世世代代或多或少、或深或浅地理解了“和”的精神。我们才能够兼容并包，使各种思想能够和谐共处，孔子的思想具有普世价值，人类21世纪最需要的思想是孔子的思想。 <br />
<br />
　　孔庆东在接受记者采访时表示，看到两岸学子对孔子思想了解得很深刻，觉得激动又欣慰。我们处在一个全球化的时代，全球化是以经济一体化为主的一个浪潮，在这个过程中，很多精神层面的东西被大家忽视，产生精神危机，用老话说就是“礼崩乐坏”，这不是一个国家一个地区的现象。有识之士都在关注，用什么方法，一方面保持经济发展，一方面又能维持人们心灵的健康，正在这个时候，我们才会重新发现孔子的价值。<br />
<br />
　　孔庆东教授祖籍山东，系孔子第73代嫡系玄孙，日前应邀参加第二届中华文化快车暨两岸师生联合祭孔大典活动而赴台，昨天在台湾新竹县体育馆，面对万余来自孔子故里山东、北京、以及台湾新竹的师生及各方嘉宾、教育界人士发表文化演讲。 <br />
<br />
　　《中华文化大讲堂》是中华文化快车系列活动之一，今年为第二届，由台湾忠信文教基金会、台湾忠信学校、北京红枫文化传播公司承办。每年和两岸师生祭孔大典一起举行，为继承与发扬中华民族优良传统文化，增进两岸青少年学生及两岸教育工作者的文化交流发挥了积极作用。 <br />
<br />
　　据台湾忠信文教基金会执行长高天极先生介绍，去年《中华文化大讲堂》主讲人为北师大教授于丹，赴台湾新竹作文化演讲，当时岛内由孔子第78代玄孙，忠信学校毕业生孔维暄代表迎接，时人誉为一个“穿梭时空的美丽相遇”，目前孔维暄同学已经进入台湾清华大学电机系深造，得知来自北京的本家长辈孔庆东教授将来演说，欲出席旁听，此次大典便仍然请她代表忠信学校迎接孔庆东。二孔本家跨海相遇，所有与会嘉宾有缘一同见证这一场“中华血缘的神奇之旅”。 （李杰）<br />
