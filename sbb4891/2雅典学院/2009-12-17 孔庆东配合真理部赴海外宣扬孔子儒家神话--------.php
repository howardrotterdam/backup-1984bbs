<br />
孔庆东认为许多中国读者在思维模式上存在问题，“人们总是无法从课本中退出来，一看没武打就觉得无趣，而许多外国留学生反而能被文章所感动”，“‘祥林嫂到底是谁杀死的’其实就是把人们从课本中带出来了，但这只是我1991年的讲法，观众听了却觉得很新鲜，太奇怪了”。<br />
<br />
《百家讲坛》不是《幸运52》<br />
<br />
孔庆东曾把《百家讲坛》作为自己的学术阵地，但他昨日接受采访时，却表示将来不会再去了，“现在的《百家讲坛》越来越娱乐化，过度注重收视率，它又不是《幸运52》和《开心辞典》，这样发展下去不行”。<br />
<br />
孔庆东非常得意自己的一个预言，“我曾预言《百家讲坛》会变成评书场，没想到最近就收到消息说单田芳要上了”。但孔庆东并不认为《百家讲坛》是易中天“带坏”的，“易中天《品三国》依然还是以学术为依托。”<br />
<br />
“孔子就是2000年前的于丹”<br />
<br />
DD太缺少<br />
<br />
虽然对现在的《百家讲坛》不满，但对于《百家讲坛》培养出的“学术超女”于丹，孔庆东却赞赏有加，作为孔子直系传人的他，还拿自己的老祖宗打起了比方，称“倒退2000年，孔子就是于丹”。他认为，现在这种人不是太多了，而是太少了。<br />
<br />
讲论语谁都会出错<br />
<br />
于丹讲“论语”被诸多学者诟病，但孔子的这位传人却对于丹非常宽容。孔庆东认为，这么多观众喜欢于丹，是因为她弥补了知识普及上的一个重大欠缺，“有人觉得自己水平高，出来指出错误也是正常的，但不应该就此否定于丹的价值。孔子以前讲周文王，还天天被人骂呢，倒退2000年，孔子就是于丹，但是你看看现在骂孔子的人都在哪里？事实上，孔子的话也不一定每句都对。”<br />
<br />
孔子也是心灵鸡汤<br />
<br />
孔庆东认为孔子的高明之处，其实在于他思想层次丰富，他既有高深的思想，也有让人倍感亲切的内容，“从某种意义上，孔子和于丹都是起着心灵鸡汤的作用，励志只是孔子思想精髓的一部分”。</div>

							
							
							
							
