													<h2>八卦一枚：胡耀邦家人如何看待温家宝</h2>
						
						
																											<div id="postmessage_829541" class="t_msgfont">作者：斯夫 <br />
最近温家宝在深圳谈政改，又引起了大家对他是不是真想搞政治改革的争论。半年前，温家宝就因为写文章纪念胡耀邦引起过类似的争论。这些争论还进一步引申到对温家宝品行的质疑，甚至还给他起了个“影帝”的绰号。那么温家宝到底是个什么样的人呢？胡耀邦家人对他的看法，也许能给大家一些参考。<br />
 <br />
在今年4月15日的文章中，温家宝写到：“当年他（指胡耀邦）的谆谆教诲我铭记在心，他的言传身教使我不敢稍有懈怠。他的行事风格对我后来的工作、学习和生活都带来很大的影响。1987年1月，耀邦同志不再担任中央主要领导职务后，我经常到他家中去看望。……”但据接近胡耀邦家人的知情人透露，在胡耀邦家人眼里，温家宝并不如他自己所说的那样尊敬和敬仰胡耀邦，其做人做事更是离胡耀邦还是有着相当大的差距，温到胡家中的次数也不多，顶多是每年春节循例去一次而已，看望老同志及其家属，是中共高层的惯例，这种行为并不完全是因为私交。<br />
 <br />
　　这位胡耀邦家的亲友讲，温纪念胡的文章发表后，就有人向胡德平表示祝贺，并说胡老爷子当年提拔温看来还是对的。但胡德平冷笑一声说，“什么呀，他（指温家宝）哪里是纪念老爷子，除了沾老爷子的光，他什么也没做过。”知情人透露，在胡家人眼里，温家宝并不是他表面打扮的那样尊敬胡耀邦，关心胡耀邦的家人。无论是作为中办主任的温家宝，还是国务院副总理温家宝，还是现今中国最有实权的总理温家宝，平时对胡家的事并不关心，不仅对胡家的事视若不见，有时还阳奉阴违，答应的很痛快但就是拖着不办。相反，当年没有受到耀邦多少关照一些老中办的工作人员，对于耀邦家中需要办的一些事，反而很上心，办了不少力所能及的事。<br />
 <br />
　　知情人说，有这么几件事，可以为温与胡的关系做注脚：<br />
 <br />
　　一是胡耀邦下台。1987年1月，胡耀邦因为“反自由化不利”被中共老人弄下台。温家宝代表中央前去胡家宣布相关规定。因为温是胡耀邦提拔的，生怕外界把他看成是胡的人，影响自己的仕途，据随行人员后来讲，温在胡家里，除照本宣科地宣读中央决定外，自始至终对胡本人及全家老人无一句安慰，连个暖人的眼神都没有。就连随行的人员都觉得有些过头。胡家人本来还希望通过温，能够对胡的医疗进一步提一些要求，但在温冷若冰霜的表情下，胡家人打消了念头。政治斗争的无情可见一斑。<br />
 <br />
　　与此相同的是，赵紫阳下台也由温家宝到家中宣布相关规定的。同样是面无表情，冷若冰霜。这些事，香港很多媒体都有报道，《开放》杂志就透露，胡耀邦和赵紫阳两家对温家宝都有近乎相同的说法：胡、赵下台后，温到胡、赵两家宣布“中央决定”，面无表情，甚至显得无情，前后判若两人，让胡、赵两家尝尽人走茶凉、世态炎凉的八字真味。<br />
 <br />
　　二是1989年耀邦去世。耀邦夫人李昭同志根据耀邦的遗嘱，提出了“只搞告别，不搞追悼会”的意见。中央怕社会反响不好，就派温家宝前去做工作。温家宝仍然是一脸严肃，一板一眼地告诫胡的家人，要看清形势，不要被阶级敌人利用。胡家人毕竟深明大义，李昭表态不再坚持原来的意见，治丧事宜一切按中央安排进行。温家宝当即就兴奋地向时任总理的李鹏进行汇报、表功。胡家人才明白，温已经投靠了邓小平、李鹏。这件事前些日子美国出版的《李鹏六四日记》中也有记载。<br />
 <br />
　　三是2005年胡耀邦同志90周年诞辰纪念活动的事。2005年春节，胡锦涛一大早向胡夫人李昭拜年，表示今年是耀邦九十诞辰“中央会有考虑”。接着胡锦涛指示由中办主任王刚和副主任令计划两个人牵头，召集了“隆重纪念11月20日胡耀邦同志90周年诞辰”多方参加的协调会，宣布11月中央将举行胡耀邦九十诞辰纪念活动。当胡锦涛将纪念胡耀邦的活动的方案提交政治局常委讨论时，发生了尖锐的斗争，九位常委中有四个人表示反对，他们是黄菊、李长春、罗干，还有一个就是曾在胡耀邦手下工作过的温家宝。他们一致反对的理由是：“纪念胡耀邦，六四怎么办？赵紫阳怎么办？”胡锦涛说：“你们先不要说六四、赵紫阳，你们先说说你们个人对胡耀邦同志的意见。”黄、李、罗三人先后说：“我们和胡耀邦同志没什么接触，没什么意见。”胡锦涛说：“那既然没意见，就应该搞。”但温家宝仍坚持说赵紫阳怎么办？胡锦涛说：“胡耀邦同志已经去世十六年，赵紫阳刚去世，可放一放，以后再说。”在胡锦涛的力主之下，11月18日，中共中央在北京人民大会堂举行座谈会，纪念胡耀邦同志诞辰90周年。座谈会由吴官正主持，温家宝出席座谈会，曾庆红在会上发表重要讲话。知情人称，由于这件事前后有温家宝等人阻挠，耀邦夫人李昭很有意见，李昭借故出席江西共青城纪念活动，没有出席这次座谈会。<br />
 <br />
四是《胡耀邦纪念文集》出版搁浅一事。2007年，胡德平和胡耀邦的一些部旧通过百般努力，编好一本《胡耀邦纪念文集》打算出版。报送到中央，胡锦涛同意出版，而温家宝偏偏不同意，导致文集胎死腹中。国务院一位工作人员后来告诉知情人，温之所以不同意出版，是因为他在政治改革、普世价值方面讲了很多，已经树立了一定的民主改革派形象，而《胡耀邦纪念文集》中有大量的文字记述了胡耀邦的政治改革思想，如果一旦出版，温自己再说什么，都可能无法超越胡耀邦过去的言论，更别说实际行动了，这很有可能会淡化甚至伤害温在政治改革方面树立的形象，于是不同意文集的出版。<br />
 <br />
