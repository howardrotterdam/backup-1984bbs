　　“你说邵定中是人渣？凭什么？绝对的权力一定会有绝对的腐败吗？法国大革命时期的罗波斯皮尔在当不就被称为不可腐蚀的人吗？今天的中国谁有绝对的权力？可是腐败的人比比皆是，充斥着社会的各个阶层！就算邵定中是失败了，可他至少有改变世界的勇气。至少他努力过，至少是一个悲剧的英雄！”一个颇为激动的男子声音说。&nbsp; &nbsp;<br />
　　“我的观点是，邵的政策是对政治结构的简化，这从短时期来看可能会有效，但是从最好的角度来看也只不过是把治理的结构恢复到刚解放的时候――就是因为那种结构无法适应发展的经济和被解放的人民，中国才走到了现在这个阶段。退回去，也只能是下一个循环的开始，只是既得利益者的洗牌而已，普通百姓实在没有什么好高兴的。关键的问题是，谁能保证邵不会随心所欲滥用权力？而受害者却是整个国民！这样的教训还不够多吗？”另一个相对更为冷静男子说道。&nbsp; &nbsp;<br />
　　“哎，可是这世上还真是难有两全的法子。欧美的三权分立在权利制约平衡上是比一党专政好点。可是我们也有我们自己的现状特色。没有一个中央集权的政党统治，现今的很多边疆问题民族矛盾外来压力是很难压住顶住的……　制度与人哪个重要？我说不好，不过我想多数平凡人的民主还是好过个别优秀人才的独裁吧？”这是一个年轻女性的声音。&nbsp; &nbsp;<br />
　　“我想说的是无论民主还是专制都会有腐败和暴政产生。在民主制度下政党和政客容易为了自身的利益欺骗民众，而损害整体的利益。虽然民主可以使矫正错误容易一些，民主犯错误的机会也远大与其他。这就其本质来说是权力的拥有者和执行者不统一，信息不对称造成的。这样就造成了民主制度下社会成本过高的弊病。说到底就是到底应该由谁来掌握权力。用一句套话来说民主和专制是具有对立的统一，在人类发展的现阶段是不可能有纯粹的民主的。”先前那个激动的男子又说道。&nbsp; &nbsp;<br />
　　“我不是学者，也不会辩论。但我关心我的祖国。我说过，我是个共产主义的拥护者，但我不会加入现在的中国共产党。他是不完美的，但应该让他执政，因为今天的中国只能由他领导，只应由他领导。不同时期，情况不同。大家都知道，现在我们的有些做法，政策，是不得已的！”一个很年轻的声音插入道。&nbsp; &nbsp;<br />
　　一个大大咧咧的声音说：“要我说，什么共产主义、社会主义，都是些失败的尝试和假想。老马过于强调物质上的必然性，就是唯物过了头。人类的发展绝不是机械的滚动，并不能用单调的公式去解释和预测。巧合和人为的因素在不断对整个人类历史产生着不可估量的影响，勉强在其中找出一些规律只能作为对过去的总结，而不能用来左右未来发展的方向。”&nbsp; &nbsp;<br />
　　“各位，我想班鸣卓在大会堂内的演讲中已经说得很清楚了，不管是民主还是独裁或者任何政治体制，其最基础的问题就是一个字――”人“。区别只在于独裁是一人，而民主是万人，优秀的独裁固然也不能百分百的保证远离腐败，而优秀的民主或许也有不产生腐败的时期，说到底，政治体制的关键在于，如何才能够更好的约束腐败的产生！”&nbsp; &nbsp;<br />
　　“你也承认具体的人才是制度的活动力来源了。那么一个问题也就出现了――你的制度活动力来源的人是在制度之上呐还在其下？好的制度能在不好的制度之下出现吗？请注意我是指按制度所制度的框架体现相当多个体利益的好的制度会出现吗？就是说现实运作的时候，不好的制度容许在它的制约下改变它的不好吗？制造制度就是为了保证制度，这就是我说的机制，它是制度之先就必须遵照的东西。　”&nbsp; &nbsp;<br />
　　“我还是不太同意你把制度和人如此割裂开来。一个好的制度设计的追求应该是达到帕雷托最优，而在实现帕雷托最优的过程中，最难的不是如何去完成，而是如何去确认个体的需求。从这个角度上来说，让更多能够自我负责的个体加入这个制度，共同确立制度的目标才是必然的。换句话说，具体的人才是制度的动力和活力的源泉，而好的制度应该是相当多个体的利益追求者。当然这是在进行理论推演，不过在实际运作中，放弃一个由不完美的人组成的不完美的制度而去寻找一个完美的人代替它，恐怕还是有些天真和短视。”&nbsp; &nbsp;<br />
　　“中国的现状是肯定需要改变的，问题在于怎么改变。我认为民主和专制只是技术手段而已，而且绝对的民主和绝对的专制只能是理念而不可能在现实中存在。在这两个极端之间，为了解决现实问题有很多种组织和搭配的方案，比如公司的董事会制度，比如物业管理的业主委员会制度，比如军队中的两长制或者参谋长联席会议制度，比如三省六部等等。这些制度都是为了解决各个不同时代，不同环境下的具体问题而产生的，都是了不起的制度设计。我认为，现在中国需要组织逐级的选举，为各级立法和司法机构运作提供资金和人员……”&nbsp; &nbsp;<br />
　　班鸣卓仔细地听着，然后向路婵娟投去询问的眼光。&nbsp; &nbsp;<br />
　　“在来这里的路上，我在街上遇到一些人在讨论，便顺便录了这盘东西。”路婵娟轻声道，温柔而深情地注视着他，“现在，在学校，在机关，在工厂，在矿山，甚至在边远的农村，只要是有中国人的所有地方，人们都在进行着这样的讨论……”&nbsp; &nbsp;<br />
　　班鸣卓的目光中露出无限欣慰：“这就好了，我们的人民是智慧的，只要他们肯认真地去思索，去实践，去探索，总能找到一条真正适合中国的道路来，我衷心希望的，也正是这样……”&nbsp; &nbsp;<br />
　　路婵娟嗯了一声，点了点头。然后突然想起什么似的，打开一个保温食品盒：“来，我给你带来了你最喜欢的麻婆豆腐，还有鸡豆花，桂花藕元……”说着，她一样一样地将菜摆了出来。&nbsp; &nbsp;<br />
　　班鸣卓突然伸出胳膊，隔着桌子握住了路婵娟正在忙碌的手。&nbsp; &nbsp;<br />
　　路婵娟整个人顿时凝固，然后也紧紧反握住他的手。&nbsp; &nbsp;<br />
　　两个人就这样握着，一动不动，似乎想倾诉的一切都已通过自己的手向对方表达了……&nbsp; &nbsp;<br />
　　左面那个年轻的特警看了看表，正想说什么。右面那个年长些的特警冲他使了个眼神，微微摇了摇头。&nbsp; &nbsp;<br />
　　年轻的特警轻轻吁了口气，继续默然站了下去。&nbsp; &nbsp;<br />
　　时间一分一秒地流逝着。&nbsp; &nbsp;<br />
　　夕阳将绚烂转入窗口，柔和的金色光芒如同那无声的情怀，静静淹没了整个房间……<br />
