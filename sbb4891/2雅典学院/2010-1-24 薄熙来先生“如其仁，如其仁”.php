													<h2>薄熙来先生“如其仁，如其仁”</h2>
						
						
																											<div id="postmessage_449373" class="t_msgfont">薄熙来先生“如其仁，如其仁”<br />
<br />
近来网上对薄熙来先生的质疑、批评和攻击越来越多，有的纯属捕风捉影，有的则不乏一定的“事实依据”。凯迪网民GLT说：<br />
<br />
“子曰：仁者爱人。老B为仁者耶？生父尚可打翻在地，再踹断三根肋骨，如此不忠不孝，不仁不义之徒，只有楼主之类奸佞小人才会为之讴歌，引为同道。” （GLT 加帖在猫眼看人《打黑反贪凭铁腕，忧民爱国致良知（有关薄熙来）》文后）<br />
<br />
一位素所尊重的友人居然也拿薄熙来文革中的表现来否认薄熙来，认为东海支持薄熙来的几篇随笔“丧失了儒家立场”，这令我感到悲忧，感到有必要为薄熙来一辩。<br />
<br />
东海当然不认同大义灭亲之类邪说。无论父亲犯了什么大错大罪，儿女“灭亲”行径都是大不义。但是，一味拿薄熙来文革中的表现来说事却是一根筋的混扯。<br />
<br />
首先、过去和现在有关又有别。每个活着的人都是发展中的人，每个人的思想、认识、道德、境界都有一定的“历史局限性”也都会因时而迁与时俱进。不能拿“历史上的”薄熙来论断和否定今天的薄熙来。<br />
<br />
在那个特定的历史时期，多少大知识分子随波逐流黑白不分，误以种种大不义行径为大义，何必独独苛责薄熙来？薄熙来当时的不孝不义，更多的是思想水平问题，可以也应该批评，却不是不可原谅的。实质上，薄熙来和众多在文革中大义灭亲的红卫兵都是受了蒙蔽误导的上当受害者呀。我相信，对于过去的某些行径，薄熙来自己也是引以为耻的，我相信薄熙来早已对他父亲真诚忏悔过了。<br />
<br />
其次、私德与公德、小德与大节有关又有别。论及公德大节时，应就事论事、就“公”言“公”，私德小德可以作为参考却不能作为主要乃至唯一的标准，不能以一个人一时一事的失德否定其一切，不能无限上纲。<br />
<br />
管仲贪财怕死甚至曾临战脱逃，不仅私德不佳，某些方面的“公德”也不过关。孔子说过管仲器小、不俭、不知礼，但终究给了他相当高的评价，曰：“桓公九合诸侯，不以兵车，管仲之力也。如其仁！如其仁！”（对管仲的评价，孔子师徒已不尽一致，孔子“如其仁”之语也似不无保留，到了孟子，就不以齐桓、管仲为然矣。兹不详论。）<br />
<br />
同样，我们不能拿薄熙来的“私德”去否定他政治、社会层面的工作，也不能拿薄熙来工作中个别、局部的不足去否定打黑反贪整体上的正义性。特别是重庆立法公开官财的计划，对于当代中国来说，如果成功，那是一件空前的大功劳大公德。无论薄熙来之前干过什么坏事，犯过什么错误，无论他私德曾怎样不堪，甚至公德如何有问题，都“如其仁，如其仁！”值得东海支持，值得儒家拥护！<br />
<br />
当今中国，既得利益集团盘根错结，黑恶腐败势力上下勾结。中共扫恶反腐虽常讲不懈，早已沦为表面文章口头禅。薄熙来坐言起行，真抓实干，值得敬佩，遭到种种漫骂、遭遇巨大阻力是难免的。<br />
<br />
唯愿薄熙来先生发大雄之心大悲之愿，坚持到底，同时在打黑反腐过程中重视法治建设和制度改良----这是更加重要也更加艰难的，比起在现行法制轨道上一时一地的打黑反腐艰难多了，这也不是单靠薄熙来和重庆能够解决的，需要中央、需要体制内健康力量的大力支持和群策群力。<br />
<br />
在现有的法律制度框架内，就薄熙来先生个人现有的力量和“资源”而言，他已经做得够好、够正了。正义人士在质疑和批评的时候，不妨将薄熙来放在当代中国这个大背景下设身处地地想一想。至于不实非理的恶攻、别有用心的漫骂，那是攻骂者自己的耻辱，于薄熙来先生何伤哉。2010-1-24东海老人<br />
