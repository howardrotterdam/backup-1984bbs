其一，孔子说“如其仁”，不能简单地解为：孔子称管仲是仁者。严格地讲，在儒家，仁与圣的“级别”类同，只有儒门中的“大人”如尧舜才足以当之（儒家经典中，大人曾用来指圣人之在位者）孔子就曾自谦“若圣与仁则吾岂敢”。管仲既使做得最好，终属“外道”，况且管仲并未做到最好，所以孔子虽从事功的角度称赞他“如其仁”，语气不无保留，“要求”也不“严格”（儒家自责极严，对外道人物的评价标准则相对从宽。）<br />
<br />
其二，孔子说管仲“如其仁”，可不是管仲让“你的日子是好过了”这么简单。<br />
以仁义道德为本的文明才是真正“给人民带来长远的根本的利益”的“东西”，才能让人民的“日子”真正好过起来。好日子，不仅仅要有好的物质生活，更需要有好的精神生活和品德修养（个体）好的社会环境（群体），好的社会环境又包括好的教育环境、文化环境、法律环境、制度环境等。这一切，都中华文明题中应有之义。管仲的历史性功绩，是避免了中华文明被蛮夷毁灭。<br />
<br />
儒家经典用字遣词极有分寸（东海文章也一样），但今人因缺乏“小学”功夫，理解起来很容易出偏，如果随意加以引申，更容易失之千里。与那些喜欢随意乃至恶意地“引申”者相比，呱呱叫网友算是不错了，且很善意，故略予开解如上。<br />
