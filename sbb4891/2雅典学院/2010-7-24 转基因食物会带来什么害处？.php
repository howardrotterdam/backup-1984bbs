想不明白，你就为科学做点贡献，多吃的转基因食品，成为guinea pig我不会反对的.<br />
<br />
 <br />
------------------------你们有时间在这里看帖回帖，为什么不能花点时间去看看科普书呢，如果科普书解决不了你们的问题，可以翻看最简单的《遗传学》或者《分子生物学》。我就不跟你们探讨这个问题了。<br />
 <br />
杂交水稻没有人为的干预DNA，通过水稻之间的杂交得到人们想要的性状，而转基因是人为的控制了DNA，使产物出现人们想要的性状。<br />
<br />
，-----------------------杂交水稻如果没有改变dna，为什么水稻的性状会改变呢？？转基因没有你想象的那么牛，可以使产物出现人们想要的性状，而只能改变很小的一点形状。<br />
<br />
<br />
<br />
<br />
根据我粗浅的生物学理解是这样的：<br />
将另一个物种的基因中能够编码，能够表达的某个片段――即DNA，转接到另一个物种的基因组里面，并且使其得到表达，得到的具有新的性状的新物种叫做转基因生物。<br />
首先应该明白，脱氧核糖核苷酸是组成DNA的单体，DNA和不能编码表达的其他片段组成基因。DNA就是能够表达（表达就是通过转录，翻译生产蛋白质）的基因片段。因为基因本身可以分为编码区和非编码区，其中只有编码区上的DNA能够翻译出蛋白质，而非编码区上的脱氧核糖核苷酸序列起到了调控的作用。<br />
一开始，人们知道氨基酸有二十余种，认为氨基酸的序列里面包含着遗传信息，从而认为蛋白质就是遗传物质。但是后来通过艾弗里的肺炎双球菌转化实验&amp;蔡斯和赫尔希的噬菌体侵染实验，证明了DNA是主要的遗传物质（主要是指大部分生物，也有一些不是，比如一些RNA生物，eg 病毒）。于是，这就打破了不同生物种类之间的杂交障碍，因为科学家意识到，如果性状是由基因决定（环境只起影响作用），并且几乎所有的生物共用同一套遗传密码（也即都是用脱氧核糖核苷酸来储存遗传信息）的话，那么，只要找出方法把A生物某段基因片段移接到B生物基因组里面并使其得到表达，那么B生物就可以获得相应的性状。<br />
比如说，科学家就把水母的荧光基因移接到普通鱼的基因组里面，从而获得了能发出不同荧光的观赏性鱼。这个至少是动物之间的转基因了。也有人想象把荧光基因转移到树上面，这样的话一到晚上，路边的树就会闪闪发光，行人不怕看不清路，情侣就找不到地方偷情了，大有益于社会治安，LOL。<br />
说到食物方面，有益的当然很多，比如转基因玉米的转氨酶还是什么东西比较多，能够提高营养成分。但是既然楼主问的是有害的，我讲讲我的看法。<br />
1，DNA是不是真的就是遗传物质？之前我们认为蛋白质是，现在我们认为DNA是，以后呢？我不是说DNA一定不是，我只是提出这个可能性。有没有可能并不只有DNA是遗传物质，或者是它跟别的物质一起起作用，而那种/些物质我们还没有发现？根据现在发表的各种文献来看，有这种可能性。比如，为什么部分转基因老鼠的生殖能力比较低？比如，假如我在实际的转基因操作里面使用的各种物理方法或者药物，是不是可能改变一些物质，使得它们在一定程度上表现出遗传物质的特性？<br />
2，转基因生物所具有的特别的性状，会令它们在竞争之中获得优势。比如，转基因玉米相对于野生玉米有更好的生存能力，假如转基因玉米的花粉散播出去，生长，那么就可能造成野生玉米的减少甚至灭绝。这就造成了物种多样性的下降。一种不要紧，许多种一起出现呢？<br />
3，转基因在跨物种之间的技术的实现是否真的那么安全？比如说，把水母荧光基因转到树上面，怎么保证这个基因从在水母的器官上表达转为在树皮跟树叶上表达？在这个过程里面，真的没有发生任何变化吗？比如，除了细胞核基因，细胞质基因呢？加入荧光基因转到了大白菜上面，我们看着一盘荧光闪闪的大白菜，怎么知道它确实无害呢？<br />
<br />
总结上述说法，我的要点是：<br />
1，人是不是真的以为人可以发现，理解并且运用一种规律？是不是真的以为发现DNA是遗传物质了就可以拍胸口说This is it?是不是就认为这是“普世标准”，绝对不会有危险？我们对这个东西（遗传的本质，生态平衡的规律,etc）是不是了解得那么透彻？<br />
2，尽管从某个角度来说，生物界里面也存在着各种基因变异，而人类这么做，只不过是在加速这个变化。那么，人有没有权利去这样做？人是不是真的以为自己制造出来的新的生物不会影响整个生态平衡？而假如影响了，并且造成了不可挽回的伤害，怎么办？<br />
<br />
我觉得生物是相当复杂的一样东西，如果不是最的话。学了六年生物，还是不怎么想的明白。我是高三刚毕业的，实验班，班主任就是教生物的，他上课给我们做书本的延伸，讲些课外的知识，经常讲：“生物的东西很复杂的，说不准的。”我们在台下无奈地笑。<br />
楼上的 主席下一小兵 说：“现在反转基因的同学就跟百年前的天朝人反火车说铁路冲了风水是一路货色。”怎么说呢，我也算是半个反转基因的。我的态度是，在没有了解透彻之前，还是要缓些动手，至少，少一点动手，不要急着大范围推广。但是什么才算是了解透彻了呢？我也不知道。百年前他们铁路没有冲掉风水，可是，你看铁路现在遍布大江南北，带来了别的什么恶果？还是有的，对吧。我不是说我愿意留在那个时代，但是，假如我们选择了让铁路进来，我们就回不去那个时代了。转基因也是这样的，或许它是一个趋势，我们终究要选择它，但是发现它带来了别的我们不想要的恶果的时候，我们还能回到现在吗？<br />
我也不清楚。你们觉得呢。<br />
<br />
-------------------------------------你觉得什么样的东西才是真正的规律呢？牛顿运动定律在爱因斯坦的相对论出来以前，是完全正确并可以反复实践的可以信赖的东西，难道因为对某些暂时还没有合理的解释的个别现象（比如光线弯曲），我们就一定要等到相对论把这些都解释了才能应用吗，并且实际上人类永远不可能完全的理解这个自己一直以来生存的世界的。<br />
这些技术的推广是一种实践行为，只要做好了足够的实验和排查，确保一定标准下的安全，又有足够的效益，当然应该推广。而不需要去考虑纯理论的问题。如果我们不在这方面积极的推广，我们本来就不发达的农业就又错过了一种可以大幅提高生产力的机会。这两天刚刚看到美国的转基因玉米卖到中国来了，实际上在这个转基因问题上，无知者又做了一次帮凶，并且最后还是买单者。当然，jy会说这是tg的门没有把好的事，而不是阴险狡诈的md的阴谋。<br />
<br />
纯技术角度说，转基因和杂交是一样的，通过改变生物体的基因来改变其性状。这种改变基本都是没有方向选择性的，其实还是人类在做最后的筛选。都是先培育大量的样本，再从中选择几个有效的来做大面积实验，确认有效性和安全性以后在推广。实际上杂交稻也有很多的问题，比如性状不稳，容易发生基因突变等和转基因是一样的。<br />
原来的时候基因技术不成熟，杂交的效率比基因高，所以杂交做出来的产品效益比较好。后来基因技术经过不断的发展和改进，能够在某些方面比杂交的方法做的更好了，当然也就有了市场价值，值得推广了。<br />
以后的转基因技术还会不断的进步的，长远来看杂交稻技术在某些方面肯定是竞争不过转基因技术的。如果我们不发展，以后就会像现在的制药企业一样，在专利和技术上受制于人。<br />
不得不说，我们曾经在杂交技术上领先过一阵，尤其在实践上，可是在基因技术这个基础科学（网上到处都是讽刺中国基础很烂的声音）上，恐怕还是要落后很长时间的，而这种落后的程度在某种程度上和大家反对的声音成正比。</div>

							
							
							
							
