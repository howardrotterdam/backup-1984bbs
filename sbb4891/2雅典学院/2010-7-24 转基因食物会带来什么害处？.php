我个人是相信人类不可能完全理解我们所存在的这个世界的。比如一个物理学里面的量子力学，也并没有完全解决所有问题，但不失之为一个广泛适用的学科。所以我也非常同意这一句话：“这些技术的推广是一种实践行为，只要做好了足够的实验和排查，确保一定标准下的安全，又有足够的效益，当然应该推广。”<br />
但是，我认为，目前的状态就是，中国没有做好足够的实验和排查，不能够确保一定标准下的安全。在这个前提之下，即使我们知道转基因技术有足够的效益，也不应该推广。因此，你再说：“如果我们不在这方面积极的推广，我们本来就不发达的农业就又错过了一种可以大幅提高生产力的机会。“我就不同意了。如果我们明知&nbsp;&nbsp;1在目前中国这样的科研&amp;检测管制条件下，不可能保证转基因食品的绝对安全甚至只是相对安全；2假如农业要大幅度提高生产力，转基因食品的产量必定倍增；3到最后大部分全中国的人口每餐都会吃上一点转基因食品 那么，这三个假设必定就推出，4，一旦转基因食品出事，对国人的生命不利的影响将极大。我不知道你是否同意前面三个premises，如果同意的话，我看不出为什么第四个，结论，会不成立。而假如你也同意的话，那么关于“实际上在这个转基因问题上，无知者又做了一次帮凶，并且最后还是买单者。”，我就不清楚是有知者还是无知者为帮凶了。事实上，我偏向于认为大家在这个问题上一样无知，即使是生物学教授。<br />
当然了，假如出事之后，中共会怎么做？你觉得它会开诚布公地说，“对了，我们的产品有问题，大家暂时不要吃”吗？我在这方面对中共没法乐观起来。当然了，这涉及到政治，不是题内之义，不说了。但我的point是，的确有风险（对全球人都一样），而且很大（对中国人尤其是）。这跟牛顿的力学什么的有根本不同。<br />
我同意你在杂交技术曾经在全球范围内领先的观点，我也觉得转基因技术是一个趋势，现在不发展以后会受制于别人。分歧在于，你觉得，先不要管后果，走了再说；我觉得，后果可能很严重，要走，中国本身先要有一些改变。我希望我没有理解错。<br />
