																											<div id="postmessage_756489" class="t_msgfont"><div class="quote"><h5>引用:</h5><blockquote>原帖由 <i>saintdragon</i> 于 2010-7-24 01:57 发表 <a href="https://1984bbs.com/redirect.php?goto=findpost&amp;pid=756322&amp;ptid=53794" target="_blank"><img src="https://1984bbs.com/images/common/back.gif" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /></a><br />
反对转基因食物的人那么多，用转基因技术做出的产品，究竟和杂交水稻有什么不同，不是同样的改变了作物的基因吗？ </blockquote></div>让转基因技术更安全<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;・方舟子・<br />
<br />
&nbsp; &nbsp; 目前市场上的大豆大部分是转基因大豆，被转入了某种基因，让大豆具有了抗除草剂或抗虫害的能力，这样便于田间管理、减少农药使用、降低生产成本，对环境保护和农民有好处，对消费者则没有明显的好处。这类转基因作物有时被称为第一代转基因作物。<br />
<br />
&nbsp; &nbsp; 最近美国农业部批准了由杜邦公司研发的一种转基因大豆新品种的种植。这种转基因大豆油酸的含量比较高，比现有的任何大豆品种的油酸含量都高，榨出的油很稳定。通常，人们要用化学方法对食用油进行加工，通过氢化作用产生反式脂肪酸。反式脂肪酸比较稳定，便于储存，但是对健康有害。这种新的转基因大豆油由于已经比较稳定，不必再进行化学加工，不含反式脂肪酸，而且另一种对健康有害的脂肪酸――饱和脂肪酸的含量也降低了20％。像这样改良了食物的营养成分，对消费者的健康有益，能让消费者直接体验到转基因的好处的作物，<br />
称为第二代转基因作物。<br />
<br />
&nbsp; &nbsp; 抗除草剂或抗虫害转基因大豆是用“加法”研发出来的：转入一个外源的新基因。高油酸转基因大豆则是用“减法”研发出来的，让大豆种子中与脂肪酸的<br />
合成有关的一个基因不起作用，称为让基因的表达“沉默”。<br />
<br />
&nbsp; &nbsp; 国内一家一贯在抹黑转基因技术的报纸《国际先驱导报》在报道这则新闻时，把基因沉默技术称为“新一代转基因”，声称“美国正在对转基因技术进行第二次反思”，“美国的转基因技术已经转向，正从挑战天然和违背自然的发展思路转变到‘尊重天然’和‘服从自然’的框架中”。更有人声称，“美国突然推出<br />
第二代转基因新技术”实际上是以此向全世界承认“第一代转基因技术已经过时而且失败”。<br />
<br />
&nbsp; &nbsp; 实际上，基因沉默技术并不是什么新一代或第二代转基因技术。1994年批准上市的第一种转基因食品――转基因西红柿就是用基因沉默技术研发的。<br />
<br />
&nbsp; &nbsp; 以前，农民要趁西红柿还没有成熟，果实还是绿色的时候就采摘下来。没有成熟的西红柿被运送到商店后，喷上乙烯将它们催熟，变成红色再摆出去卖。这种人工催熟的西红柿没有自然成熟的西红柿好吃。<br />
<br />
&nbsp; &nbsp; 为什么不等西红柿成熟、变红再采摘呢？因为西红柿成熟后，皮也软了，运输过程中容易破。能不能让成熟的西红柿皮不变软呢？西红柿的皮变软，是因为有一种叫做多聚半乳糖醛酸酶的酶把细胞壁中的胶质给分解了。生物学家们把编码这种酶的基因克隆出来，测定了它的序列。然后合成一个和它相反的“反义基因”。把“反义基因”转入到西红柿细胞中去，会干扰原来基因的活动，让它再也没有办法合成多聚半乳糖醛酸酶，细胞壁中的胶质不会被分解掉，西红柿即使成熟了，皮也不会变软。我们就可以等到它自然成熟了再采摘，不用担心不好运输。这样，自然成熟的转基因西红柿吃起来就要比人工催熟的普通西红柿好吃，而且可以存放很长时间也不会坏。由于转入的“反义基因”只干扰聚半乳糖醛酸酶的基因，不会干扰其他基因，因此转基因西红柿的营养成分没有发生变化。<br />
<br />
&nbsp; &nbsp; 目前市场上的木瓜大部分都是抗病毒转基因木瓜，它们在研发时也用到了基因沉默技术。采用“减法”要比“加法”的健康风险更低。人们主要担心的是，<br />
给作物转入新的基因，生产的新蛋白质会不会有毒副作用或引起过敏。而采用基因沉默技术不让某个基因表达，或者采用基因剔除技术把不想要的基因去除掉，也能改变植物的性状。有时候，只增强或降低已有基因的表达也能达到我们的要求。这些做法都没有引入新的基因，因此也就不用担心新蛋白质会有问题。<br />
<br />
&nbsp; &nbsp; 此外还有别的方法让转基因变得更安全。例如，如果要引入新基因，可尽量转入其他可食用作物的基因，或者让转入的基因只在非食用的组织表达出来，就不用太担心它生产的新蛋白质能不能吃了。不久前获得安全证书的抗虫害转基因水稻就是只让抗虫害基因在非食用的部位表达，在胚乳中则不表达。所以即使你对抗虫害基因的产物的安全性有疑虑，也可放心食用，因为在米饭中不含该产物。<br />
<br />
&nbsp; &nbsp; 采用这些技术研发转基因作物新品种，并不意味着其他的转基因作物就是不安全的，只不过安全风险更低而已。国内有一位经济学教授说，不管是“基因插入”还是“基因沉默”，“后果都是不可预知的”。我不知道为何这位经济学教授自认为比生物学家更懂“真正的生命科学”。对要插入或沉默的基因的功能生<br />
物学家都有透彻的了解，转基因的结果通常是可以预知的，而且，为了避免出现未预料到的结果，还要通过生化实验和动物试验检测其安全性，采用的是最严格的食品安全检验标准。如果对这些实验的结果也要怀疑，那是在怀疑整个食品安全检验体系，而不是转基因技术。<br />
<br />
&nbsp; &nbsp; 任何食品都有不同程度的安全风险，任何技术都有可能产生不可预知的后果。转基因食品迄今为之还没有发现能被确认的对健康不利的一个例子，反倒是杂交技术曾经培育出对健康不利的品种，由于未经检测就上市，后来发现问题了再召回。不知为何那些反对转基因的人士对没有受到同等的严格管理并且出过问题的传统作物反而那么放心？<br />
<br />
2010.7.19.<br />
<br />
（《中国青年报》2010.7.21）</div>

							
							
							
							
