													<h2>不是旗手是杀手 不是文豪是文妖――鲁迅批判</h2>
						
						
																											<div id="postmessage_690687" class="t_msgfont">不是旗手是杀手，不是文豪是文妖----鲁迅批判<br />
<br />
一<br />
十多年没有翻过鲁迅全集了。在旧书摊淘到一本1974年文物出版社出版的《鲁迅反对尊孔复古言论选辑》一书，闲翻一回，仿佛闻到了文革的硝烟烈火，对鲁迅思想的糊涂、混乱和颠倒亦有了进一步的认识。<br />
<br />
鲁迅对儒家与孔子的抨击十分猛烈，一付势不两立的姿态。他将国民劣根性的根源、将历代专制主义和特权阶级的罪恶统统推在中华文化特别是儒家身上，予以全方位、整体性的排斥攻击与否定。<br />
<br />
鲁迅劝青年少或者竟不看中国书，多看外国书。他说：&quot;古书实在太多，倘不是苯牛，读一点就可以知道，怎样敷衍，偷生，献媚，弄权，自私，然而能够假借大义，窃取美名&quot;云云。<br />
<br />
对儒佛道经典“读一点就可以知道”，这话完全是诬蔑。借用鲁迅的话说：不知道而这么说是可恕的，否则，当得永远的诅咒！且不说儒佛道经典，其它大部分古书何尝如此这般？<br />
<br />
鲁迅说：“所以要中国好，或者倒不如不识字罢，一识字，就有近乎读经的病根了。”“解放初”和文革时多数领导干部倒确是“不识字”不读经了，而且以“大老粗”为荣，以“大革文化命”为职责。然而中国不但没好起来，反倒被“干”成了个“率兽食人”的丛林社会、禽兽世界！<br />
<br />
二<br />
在鲁迅看来，&quot;仁义道德&quot;就是&quot;吃人&quot;，传统的承担者&quot;话中全是毒，笑中全是刀&quot;，&quot;就是吃人的家伙&quot;，&quot;中国的文化，都是侍奉主子的文化&quot;。他说：&quot;所谓中国的文明者，其实不过是安排给阔人享用的人肉的筵宴。所谓中国者，其实不过是安排这人肉的筵宴的厨房。不知道而赞颂者是可恕的，否则，此辈当得永远的诅咒！&quot;<br />
骂得倒痛快，可是事实如何？数千年的中华文明，数不胜数的仁人义士，就这样被一笔抹杀了。确实，古今中外都有很多不仁不义的东西冒充道德，中国古代也不例外。如果说伪道德吃人，伪君子&quot;就是吃人的家伙&quot;，那没有问题。但说道德吃人、君子吃人，就混扯了。<br />
<br />
君子必不伪，伪必不君子；道德不吃人，吃人不道德。这样的常识不难理解。反对伪君子，反对伪道德，反对君主时代的某些落后过时的道德规范，都很正常很应该很有必要。但要把正人君子和道德本身一块反掉，这就不正常不应该了。<br />
<br />
（对于某些落后的道德礼仪规范，反对是应该的，不过，既然已经过时，就是已经不存在了，没有人再遵守了。而今，一些人仍在大反特反，其实不过是想借此抹黑儒家罢了。）<br />
<br />
把真道德真君子与伪道德伪君子一起统统打倒，以真小人真恶人为荣的时代就到来了，地痞流氓必纷纷走上历史舞台成为“食人者”，食起人来更加赤裸裸，更加肆无忌惮。<br />
<br />
鲁迅说：“这人肉的筵宴现在还排着，有许多人还想一直排下去。扫荡这些食人者，掀掉这筵席，毁坏这厨房，则是现在的青年的使命！”话说得很好。可是，如果“现在的青年”基本道德，又如何来“扫荡这些食人者，掀掉这筵席，毁坏这厨房”？乱扫一通甚至加入“食人者”队伍的概率更高。<br />
<br />
可悲的是，诸如此类的胡说八道，居然被不少人赞为“深刻”，捧为经典，不仅受到毛泽东们的高度推崇，而且至今彩声不绝。<br />
<br />
三<br />
其实鲁迅自相矛盾的地方很多。例如鲁迅说过：“我们从古以来，就有埋头苦干的人，有拚命硬干的人，有为民请命的人，有舍身求法的人，……虽是等于为帝王将相作家谱的所谓“正史”，也往往掩不住他们的光耀，这就是中国的脊梁。”<br />
<br />
这些中国的脊梁，不正是传统文化特别是儒家文化培养出来的仁人义士么？他自己就说过：“汉朝以后，言论的机关，都被‘业儒’垄断了。宋元以来，尤其利害。我们几乎看不见一部非业儒的书。听不到一句非业儒的话。除了和尚道士，奉旨可以说话的以外，其余‘异端’的声音，决不能出他卧房一步。”照此说法，至少“汉朝以后”，绝大多数“中国的脊梁”免不了受儒学的熏陶吧。<br />
<br />
就拿鲁迅评价很高的屈原来说。鲁迅在《摩罗诗力说》中对屈原的推崇备至，在《汉文学史纲要》中称赞屈原：“被谗放逐、乃作(离骚》，逸响伟辞，卓绝一世。然其影响于后来之文章，乃甚或在三百篇以上。”殊不知屈原“独立不迁”、“横而不流”、“秉德无私”等精神，与儒家思想一脉相承。<br />
<br />
孔子（比屈原早约两百年）“志士仁人，无求生以害仁，有杀身以成仁”、曾子“士不可以不弘毅，任重而道远”，孟子（比屈原年长约三十岁）“天下有道，以道殉身；天下无道，以身殉道”等等话语，无不可以涵盖屈原精神。<br />
<br />
另外，鲁迅心仪魏晋风度，在广州做过长篇演讲：《魏晋风度及文章与药及酒之关系》，对嵇康阮籍刘伶辈赞赏有加。嵇康阮籍刘伶辈不也是中华文化的“产品”么？<br />
<br />
鲁迅说，无破坏即无新建设。或许他的破坏是为了更好地建设，为了创造新价值或者向外吸取新价值。然而事实证明，他没有创造任何新价值也没有向外吸取新价值。对于文化和价值，他只是一个破坏者，而且是最为粗暴恶劣的“盗贼式的破坏”。<br />
<br />
鲁迅被抬举为“文化革命的旗手”、“共产主义者”、“无产阶级革命家”，固然有被利用的成分（这是鲁迅的大不幸，其次，鲁迅生前死后都没有受到严肃、中肯而深刻的批判，这也是他的不幸----当然更是中华文化、中华民族的大不幸。）其思想也不乏被众多利用者歪曲的地方，但鲁迅本身即使不被歪曲，他的认识照样是错误的，错误照样是严重的。<br />
<br />
对于“新中国”的种种人祸，种种劫难，鲁迅难辞其咎。何以故？<br />
<br />
倘儒家不倒，民主自由的“落实”才有坚实的道德基础和义理基础，儒家的民本可以借助现代民主得到制度性的保障。<br />
<br />
倘儒家不倒，不把中国弄成文化废墟，马克思主义就难以乘虚而入；不打倒儒家，各种违反人性悖逆道德匪夷所思的政治行为就不可能畅通无阻，“文化大革命”之类疯狂的运动就“动”不起来。<br />
<br />
倘儒家不倒，利己主义、拜金主义绝不可能泛滥成灾。墨子的利他主义（与所谓的“毫不利己专门利人”的“共产主义道德”类似）与杨朱的利己主义，两千多年前就都已经遭到孟子的严厉批判。对于利益至上的拜金主义，儒家更是天敌。<br />
<br />
四<br />
遗憾的是，儒家毕竟被打倒了，鲁迅则不愧是打倒儒家的第一功臣。用周建人的话说：“鲁迅批判孔家店是最彻底、最坚决、最全面的。”<br />
<br />
难怪鲁迅会与毛泽东惺惺相惜。鲁迅在信中对毛泽东表示“我得引为同志，是自以为光荣的”。毛泽东对鲁迅更是推崇有加，称之为“中国第一个圣人”、“共产主义者”、“中国文化革命的伟人”等等。在《新民主主义论》中：毛泽东更是满怀崇敬与激情地唱道：<br />
<br />
“而鲁迅，就是这个文化新军的最伟大和最英勇的旗手。鲁迅是中国文化革命的主将，他不但是伟大的文学家，而且是伟大的思想家和伟大的革命家。鲁迅的骨头是最硬的，他没有丝毫的奴颜和媚骨，这是殖民地半殖民地人民最可宝贵的性格。鲁迅是在文化战线上，代表全民族的大多数，向着敌人冲锋陷阵的最正确、最勇敢、最坚决、最忠实、最热忱的空前的民族英雄。鲁迅的方向，就是中华民族文化的方向。”<br />
<br />
或许鲁迅主观上居心不坏，但是鲁迅客观上充当了马克思主义进入中国的思想先锋，为“解放”后各种运动特别是“文化大革命”提供了最佳理论武器，也间接地为今天利己主义拜金主义及各种异端外道歪理邪说的大泛滥扫清了文化障碍。<br />
<br />
以中华文化为敌、视仁义道德为敌而冲锋陷阵最正确、最勇敢、最坚决、最忠实、最热忱，不是文化杀手是什么？把儒佛道都打倒了，把文化的命革掉了，还奢谈什么“中华民族文化”？国有劫难，必有妖孽。鲁迅不啻为千古难逢的文妖，继之而起的浩劫能不旷世空前乎？<br />
鲁迅的骨头或许最硬，他或许没有奴颜和媚骨，但这说明不了什么问题。杀手的<br />
骨头也可以是很硬的。何况，别说在孔孟之道面前表现硬骨头不算什么，躲在租界里骂骂当时的军阀及当局也谈不上骨头最硬，比后来的梁漱溟先生、比古今众多仁人义士差远了。<br />
