																											<div id="postmessage_856302" class="t_msgfont">贺卫方 谈三个至上&nbsp;&nbsp;摘录<br />
<br />
眼下全国司法界正在“大学习、大讨论”，其中的核心内容就是贯彻“三个至上”学说：始终坚持党的事业至上、人民利益至上、宪法法律至上。报章连篇累牍，会议连绵不绝，最高法院甚至还组成宣讲团在全国巡回宣讲。我对此感到很困惑，因为这“三个至上”在逻辑上存在着值得商讨的地方，尤其是把它运用到司法领域时，这样的逻辑困难会给司法界带来某些混乱。<br />
<br />
 <br />
<br />
很明显，所谓“至上”，便是最高的准则。至上有其惟一性，不可以有多个准则一块儿至高无上。好比在一个家庭里，假如有一个人说话具有最终极的权威，那么就不可能是公公的意志至上，婆婆的意志同样至上，媳妇的意志还是至上。又好比奥运会比赛，冠军只能有一个。一下子出现三个冠军就摆不好――领奖台齐平，同时奏三个不同国家的国歌，岂不是乱了套，或者用时髦的说法，就要“俯卧撑”，或者如正龙拍虎一般扑朔迷离！因此，如果党的事业、人民利益以及宪法法律是具有不同内容、相互区别的三个事物，那么我们必须给出一个顺序，按照法律界熟悉的说法，要对于三者的效力位阶作出安排。例如，或许可以如此表达：当法官处理案件时，需要对于上述三者进行考察，当三者统一时，应依据宪法和法律作出判决；当三者有矛盾时，应将人民利益置于宪法法律之上，将党的事业置于人民利益和宪法法律之上。也就是说，在效力位阶上，党的事业最高，人民利益次之，宪法法律再次之。<br />
<br />
 <br />
<br />
这样的说法在逻辑上没有问题了，但是在我们的政治语境下却是不成立的。因为按照官方一贯的话语模式，在我国，这三者是一而三、三而一的。十七大修改通过的《党章》在总纲里明确地说：“党除了工人阶级和最广大人民群众的利益，没有自己特殊的利益。”在一次领导人的讲话中，我们读到这样的说法：“我们党领导人民制定了宪法和法律，也要领导人民遵守宪法和法律。宪法和法律体现了党的主张和人民意志的统一。党员遵守宪法和法律就是遵从人民的意志，服从党的领导。所有的党组织、党员尤其是负责干部的言行，都不得同宪法、法律相抵触。加强党的领导同充分发扬民主和坚持依法办事是一致的。”（《十三大以来重要文件汇编》中册页943-4）既然三者之间毫无区别，那么不厌其烦地把三者并举，而且要“三个至上”，岂非修辞上的同语反复？</div>

							
							
							
							
