																											<div id="postmessage_856807" class="t_msgfont">关于作了相当多的描述，要自已去理会；<br />
<br />
法律面前人人平等这一基本原则就因现代社会主义先驱者所提出的种种新的要求而受到了威胁，这是因为他们要求以事实上的平等（an egalite de fait）取代只是法律上的那种平等（a mereegaliie de droit）。<br />
<br />
区别一个自由的社会与一个不自由的社会的判准乃是，在自由的社会中，每个个人都拥有一个明确区别于公共领域（public sphere）的确获承认的私域，而且在此一私域中，个人不能被政府或他人差来差去，而只能被期望服从那些平等适用于所有人的规则。在过去，自由者可以夸耀地说，只要他们处在众所周知的法律范围内，他们的行动就可以不需要征求任何他人的许可或服从任何他人的命令。然而当下的我们是否还能够做这样的宣称，已属疑问。<br />
<br />
真正的法律的第三个要件乃是平等（equality）。它与上述属性具有同等的重要性，但在界定方面却要比它们困难。任何法律都应当平等地适用于人人，其含义远不止于我们在上文所界定的法律应当具有的一般性的含义。一项法律可能只指涉相关的人的形式特征，因而它在这个意义上具有着充分的一般性，然而它却仍可以对不同阶层的人做出不同的规定。显而易见，即使在具有完全责任能力的公民中，进行这样的类分也是不可避免的。<br />
<br />
我们必须承认的是，尽管人们为解决这个问题做出了诸多切实的努力，但迄今为止人们尚未发现一个能完全令人满意的标准，亦即那种能告知我们何种类分是与法律面前人人平等的原则相符合的标准。一如人们通常所说的，那种宣称法律不得确立不相关的类分标准的说法，或者那种宣称法律不得根据与此一法律的目的毫无干系的理由将人做差别对待的说法，无异于对实质问题的回避。 &lt;br /&gt;<br />
<br />
虽说法律面前人人平等只是诸理想之一，它能指示方向但却不能完全确定目标，从而也始终是我们的能力所不及者，但是这一理想却不会因此而丧失它的重要意义。我们在上文已经论及了法律面前人人平等的原则必须满足的一个要件，亦即是说这种界分的合法性必须得到经选择而确立起来的某一群体中的人与此一群体之外的人的共同承认。我们可以追问我们是否能够预见到一部法律影响特定人的方式，这一点在实践中极为重要。然而，法律面前人人平等的理想，乃旨在平等地改善不确定的任何人的机会，它与那种以人们可预见的方式致使特定的人受损或获益的做法都是极不相容的。</div>

							
							
							
							
