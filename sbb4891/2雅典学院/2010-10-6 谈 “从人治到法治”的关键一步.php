<br />
<a href="http://zh.wikipedia.org/zh-cn/%E6%B3%95%E6%B2%BB" target="_blank">http://zh.wikipedia.org/zh-cn/%E6%B3%95%E6%B2%BB</a><br />
<br />
法治（Rule of Law），是指在某一社会中，法律具有凌驾一切的地位。所谓‘凌驾一切’，指的是不单止任何人都必需遵守、甚至是管治机构的包括制订者和执行者本身亦需要，而法律本身亦被赋与一个非常崇高的地位，不能被轻慢。政府（特别是行政机关）的行为必须是法律许可的，而这些法律本身是经过某一特定程序产生的。即，法律是社会最高的规则，没有任何人或机构可以凌驾法律。<br />
法治这个理念早在远古时候已出现，不同的社会及文化对法治的具体理解亦有些微分别。在英国，著名的法律学者戴西(Albert Venn Dicey)在其著作“Introduction to the Study of the Law of the Constitution”具体的指出法治包含了三个基本完素。<br />
<br />
第一个元素指出，没有有关法律前，没有人会因为未作出违反该法律的行为而受到惩罚，或是在肉体上或财物上有损失。亦即是说当权者不能有肆意的权力 (arbitrary power)，也不能在有人作出某行为后作出有追溯性(retrospective)的法律修改而惩罚某人。一个有名的英国案例Burmah Oil Co. Ltd. v Lord Advocate (1965)正好反映当年英国政府违反了有这个法治元素。事缘二次世界大战时英军在承诺Burmah Oil 公司会作赔偿后，炸毁其公司在缅甸的油田及设施，为要在其撤退后不让日军得到有关战略性设备。可是在战后英政府却拒绝Burmah Oil公司的赔偿要求，尽管英国政府在终审法院(House of Lords)输掉了官司。后来英国政府运用其影响力在国会通过一条名为War Damage Act (1965)的新法例，指出政府并不需要为战争造成的损害而作出赔偿，而此条例是有追溯性的，并可追溯至二次大战时期。<br />
<br />
第二个元素指出，没有人能凌驾于法律上，包括所有男女，且不论其社会地位或其情况。<br />
<br />
而第三个元素指出，法庭的决定是维护个人权益的最后防线。<br />
[编辑] 法治的重要性<br />
<br />
现代大部份国家都认同法治的重要，虽然他们产生法律、立法代表的方法不太相同。法治的好处在于能防止有特定的个人或政府权力凌驾法律而伤害其他大多数人的利益。<br />
<br />
就民主制度而言，法治是使民主制度能发生实际效力的方法。就人权保障而言，法治则使普世价值的人权和各国宪法或基本法上的基本权利不受来自他人或政府的非法侵犯。</div>

							
							
							
							
