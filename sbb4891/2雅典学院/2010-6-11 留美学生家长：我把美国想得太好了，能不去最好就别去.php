																											<div id="postmessage_675150" class="t_msgfont">海不归老爸：我把美国想得太好了，能不去留学最好就别去<br />
<br />
文：彭世军<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;“到美国留学并不是像想像中那么好，能不去最好不要去。”15日晚，刚从美国探望儿子回来的市民王先生，在接受本报记者采访时，以儿子在美国留学的亲身经历，告诫成都家长。他是看了本报关于“美国教育到底好不好？”的报道后，拨打本报热线参与讨论的读者之一。 <br />
<br />
这是一位64 岁的退休职工，其儿子清华大学毕业后，于2000年留学美国，获得双硕士学位，握有美国绿卡，但迟迟不愿意加入美国国籍。<br />
<br />
留学生们一般都“报喜不报忧”<br />
<br />
　　“我以前对儿子在美国留学的情况不了解，他每次总是报喜不报忧，说在那边一切都很好，但我前不久去了美国，才发现儿子在那边并不是像他说的那么好。”王先生告诉记者。他儿子10年前清华大学毕业后，到美国加州伯克利大学读硕士。毕业后进入某知名大公司工作，谁知又遇上了金融危机而再次失业。失业后他再次进入大学读金融硕士，如今在美国一家公司工作。<br />
　　“儿子到美国 10年了，至今租住在公寓房，领着10万美元的低年薪。而像他一样的留学生还有很多，也有很多人在干着餐厅打工一样的低薪水工作，还有很多人失业找不到工作但他们却不愿意回到国内。”<br />
<br />
留学生们收入低 但美国的消费很高<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;“我儿子在美国如今的年薪有10万美元左右，税后只有6.5万美元，一般人看起来好像不错了，但这点年薪，在美国根本不算啥，仅仅能养活自己。”王先生告诉记者，美国的物价很高，房租一月2400美元，加上水电费等达到了3000美元，但他每月的收入仅有5000美元左右，除了房租、水电气费、生活费外，每月基本没有节余。“他妻子去年4月失业后，一年多都没有找到工作，没有一分钱收入，仅仅靠儿子的微薄工资，勉强养活一家四口。至今10年过去了，他在美国除了有一辆廉价的车外，没有房子，其他什么都没有。”<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;美国的物价很高。“前不久我跟儿子去吃了一顿牛排，牛排65美元，加上停车费17美元、小费10美元，一顿饭花了近200美元。我说我们一顿饭吃掉了1000多元人民币啊。在国内，吃牛排哪里需要1000 多元啊？在美国，一个盒饭8美元，一盒方便面5美元，是很贵的了。在国内哪里需要这么贵啊？美国人一般都吃汉堡，也需要5美元。”<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;“在美国生不起病，医疗费也很贵。一个小感冒，挂号费就是200美元，如果挂专家号，需要800美元。补个牙齿需要5000美元，你敢去补吗？如果你生个大病，那更是治不起。”<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;王先生说，儿子清华大学的同班同学，现在很多都混的很不错了。有的已经当上了博导，房子车子票子都有了，但儿子如今还一穷二白。他如果不出国，至少也是一个博士了，可以得到很好的发展。<br />
<br />
在美国 留学生找工作很难<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 跟儿子一起去美国留学的，还有他清华大学的几个同班同学。清华大学的学生在国内算牛吧？但到了国外，你啥都不是。“如今几个同学都在一个公司工作，大都混得不好，收入不高。”<br />
&nbsp; &nbsp;&nbsp; &nbsp;“我儿媳是个成都人，大学毕业后直接去了美国留学，如今已经加入美国籍。她的收入情况更糟糕，前几年年薪只有3万美元，扣除35%的税后只有1.9万美元，是很低的收入了。在美国，开卡车的收入都在7、8万美元，即使一般人的收入也有 5、6万美元。”<br />
<br />
“因为金融危机，她去年4月下岗了，开始领救济金，但领到9月，救济金就被停发了。”一年来，儿媳到处找工作，面试了几次，但都失去了机会。如今整天在家照顾孩子，家庭支出全靠儿子的5000美元收入度日，生活还是很艰难的。&quot; <br />
&nbsp; &nbsp;&nbsp; &nbsp; “我多次跟儿子说，干脆回国算了。你那些清华大学的同学，现在在国内都已经很发达了，当官的、开公司的都有，你如果留在国内不出国，如今也会跟他们一样，成为社会的主流精英。至少房子车子会有了，不像你在美国，房子都没有一套。”但儿子坚决拒绝了他的要求。儿子告诉他，他在美国生活的很好，虽然收入不高，但活得很自由，不用看别人的脸色。“国外的空气好得多，我至少可以多活几年，国内的空气污染太厉害了，人事关系太复杂了，我不习惯那样的环境。”<br />
王先生仔细给记者分析了儿子不愿意回国的原因。他说，一是儿子觉得在美国没有挣到钱，如果回国，会很没有面子。另外就是他已经彻底美国化了。“他每天要喝咖啡，吃西餐，交流都是英语，儿子他总觉得在美国很自由，我跟他已经没有共同语言。” <br />
<br />
美国教育好，但那是有钱人玩的<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;美国的教育是很先进，但费用也很高，一般人还是承受不起的。王先生认为，到美国留学，可以学习很多先进的东西，比如遵守交通规则，有环保意识和爱心，懂得帮助别人。“在美国开车，你不能超车，在行人面前要主动停车，让行人先过。美国的校车权利很大，接送孩子上车的时候，所有车辆都要停下来，让孩子们通行。校车司机还可以直接对违章的车辆开罚单。美国人的规则意识很强，这些都是美国教育的结果。”<br />
王先生参观过麻省和哈佛，给他留下了深刻印象。“哈佛的校园绝对没有北大清华漂亮，校园建筑都是100多年前修的，看起来很旧了，但人家培养出来的学生，却能成为政治界、经济界的名人。像哈佛大学，已经是美国经济的风向标，学校的很多经济学家都是美国政府的经济智囊。”<br />
　　王先生在美国与很多人接触过，了解到美国教育的一些先进的地方。他说，美国教育，很注重学生的动手能力、社会活动能力培养。“他们的课堂都是讨论式的，通过一些具体的游戏、项目和调查，培养学生的粉丝思考能力和动手能力，不像我们，一味要求学生死读书。”<br />
　　王先生认为，美国大学生牛，牛就牛在人家的教育，让学生能够树立自信心，能够培养他们的爱心和社会责任感。<br />
<br />
美国教育好，但能不去最好就不去<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;“美国教育虽然好，但国内的学生能够不去留学，最好就不要去，现在国内发展得这么好，学生们在国内一样可以得到很好的机会。”王先生告诉记者，一是美国的失业情况很严重，很多大学生找不到工作，失业率已经达到10%左右。而是即使找到工作，也没有好工作，收入一般也只有5、6万美元，这点工资在美国根本不算啥。但如果在国内，大学毕业也能领到月薪2-3000元左右，在国内可以生活得不错。<br />
<br />
美国大学的学费也很贵，如果申请不到奖学金，一般家庭是很难承受的。“美国大学的学费，一般在每年2万美元左右，加上生活费、交通费、交际费等，一年至少也需要3万美元，4年下来就是12万美元，相当于80万人民币了。”<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;“其实国内现在经济高速发展，机会比美国更多，一样能让自己得到很好的发展。”王先生告诉记者。“儿子的清华同学，有的在2001年就达到了年薪12万，现在则更高，有的已经从政，有的已经成为公司老总，发展得都很不错。现在很多华人和留学生不愿意加入美国国籍，他们就是看到美国现在一天不如一天，希望以后回国发展。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;“我以前把美国想得太好了，以为那里满地跑的都是宝马、奔驰，结果这次去了，才发现其实像捷达、尼桑之类的低档次车也满街都是，大街上也有乞丐行乞，美国的治安环境也不好，芝加哥一到晚上8点就看不到人。街上也经常发生抢劫案，警车经常大呼小叫。”<br />
