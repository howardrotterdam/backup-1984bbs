													<h2>一个大四学生的话</h2>
						
						
																											<div id="postmessage_274272" class="t_msgfont">上了两次课，有时会在课堂上讲点书本外的东西，于是有学生通过网上交作业的方式，这样对我说：<br />
<br />
首先，我很诚恳地说，我不是信息员，老师放心呐~<br />
其次，感觉老师作为异见者的确是一个好的现象，但是从另一个方面说，老师作为一个异见者还能自由自在地生活在这个社会上，这是中国政治的一个进步。<br />
然后，中国一直在进步之中，所以，老师也不用那么悲观的。比如说，上回老师提到的新疆屠杀事件『不是我的原话』，我想说，那时可能中国政府做的的确不对，但是再想想现在，中国的西部大开发等等，也的确促进了当地经济的发展，改善了当地人的生活水平。我们不能因为以前的一些错误做法而一直抱有偏见。<br />
再者，说到新疆，又想起那个“我的西域你的东土”了，一样，坐着忽略了当地经济的发展，我个人觉得如果不是在中国，当地人可能更穷困。当然，这里也要说明，我也不是民族主义者，并不歧视少数民族，但是在“7.5”事件中，我们无法知道缘由是什么，但是可以肯定的是，平民受伤了，这从任何一个层面上来说都是是不对的！<br />
最后，反问老师一个问题，老师在第一堂课上说中国是目前唯一一个“主权大于人权的国家”『原话自然不是如此的，唯一之类的我肯定慎用』，这句话我觉得也是有纰漏的，那美国那么发达，人权发展的很好吗？不好！关塔那摩，伊拉克，阿富汗，如果让美国分裂一个阿拉斯加出去，他肯吗？<br />
最最后，我们必须得承认外国发达国家在很多方面的确很好，想对中国来说，但是，人家已经建国上百年，有些财富甚至是从中国晚清时掠夺而去的，而中国，才刚刚过完60岁的生日，这对于一个国家来说，还只是少年时期，但是，他一直在进步，恩！所以，老师也要有信心，中国的未来会更好的~当然，如果美国幕后操作的中国周边区域不至于引起局部或更大规模战争的话。<br />
最最最后，希望以上的话不会触怒老师，谢谢，其实我也承认中国的制度目前存在很多不足，但是我们不能否认有人在努力，当权者也不希望这样的~<br />
