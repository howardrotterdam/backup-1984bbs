																											<div id="postmessage_443226" class="t_msgfont">不只是最牛的视频，剧本，简直是一部细察了当代中国专制陷阱、近年国进民退状况，以及80后--90年代人对抗专制惯用方法的集大成之作。<br />
我甚至觉得超过了所有的进取性媒体这些年的折腾忙碌，胜过一切习惯春秋曲笔的专栏作家。已经不是一个游戏视频那么简单。真实来讲，震撼程度超过《阿凡达》。<br />
<br />
我很不希望这样说，但就感染力、攻击力和大胆程度以及完全网络的叙述方式，酣畅淋漓的控诉、犹如震雷般的草你妈，我看不出中国这10年有哪个载体如此高密度的抗争表现，并具有非常的观赏性。<br />
