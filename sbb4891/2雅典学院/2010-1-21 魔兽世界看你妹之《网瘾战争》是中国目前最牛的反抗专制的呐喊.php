<br />
我同时确信视频团队只是非常单纯的发泄和表达，他们可能都不会来1984类似的论坛。但就如同连岳所说，韩寒懂得从简单出发，看事情因此透彻。我觉得这个团队的作者也是，只是爱游戏的聪明者、想呼吸自由的玩家们。<br />
我很不希望TG用我的角度来看这个视频，但愿视频制作团队不会受到特殊的境况。只好祝福了。<br />
