													<h2>韩寒：十块钱更实在</h2>
						
						
																											<div id="postmessage_531637" class="t_msgfont">转载自：韩寒新浪博客，<a href="http://blog.sina.com.cn/s/blog_4701280b0100hjvm.html" target="_blank">http://blog.sina.com.cn/s/blog_4701280b0100hjvm.html</a><br />
<br />
1：<font color="blue">有没有最近推荐的电影</font>？<br />
 <br />
&nbsp; &nbsp;&nbsp; &nbsp; 最近看了两部片子，《战争之王》和《别人的秘密》，后者中国的翻译应该是《窃听风暴》，我一直没有看是因为把他和香港的窃听风云搞混了。前者是一部很不错的电影，后者是一个伟大的电影。我像所有的读者推荐《窃听风暴》，至于要说点感受，那真是满纸屏蔽词，一把辛酸泪，大家还是去看吧。<br />
 <br />
2：<font color="blue">你是不是支持国产电影退票</font>？<br />
 <br />
&nbsp; &nbsp;&nbsp; &nbsp; 事实上我不支持，我说我非常支持这个想法，也理解大家的感受，但是我不支持电影看完以后觉得不好可以退票，这个不可行。就像小姐嫖过以后不能因为不爽而拒付一样，这需要你在前期选小姐的时候就有独到的眼光。事实上，中国电影发展到现在，烂片一堆，我认为制作团队虽然有不可推卸的责任，但是不能负主要责任。在一个皇上从来不书面表态，文艺工作者都小心的揣摩和回避的年代，我们已经具备了与生俱来的自我阉割功能，阉割掉的那一部分，不光是题材，良知，人性，当然还有想象力。这不怪他们。你难道不是么，我难道不是么。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 事实上，去年中国电影也并不是一无是处。《十月围城》就不错，虽然我一直很纳闷，为什么孙中山就不能改签，非要那天到。《风声》不错，还有一个相对不太有名的片子叫《斗牛》，也不错，就是剪辑给剪坏了，甚至我不太报希望的成龙的《大兵小将》也很不错，超过了期待。<br />
 <br />
 <br />
3：<font color="blue">对于谷歌退出中国，你有什么要说的</font>？<br />
 <br />
&nbsp; &nbsp;&nbsp; &nbsp; 我没什么要说的，谷歌是一个很好的公司，我的手机用的都是谷歌的系统。但是关于这件事情，你只要有一些新闻和政治敏感度，你就知道你说什么都没用，下场都不会很好，删了你的文章还算是小事。谷歌不玩了，他退去了香港，甚至可以回美国，但是作为中国的作者或者媒体从业者，如果说了一些人话，他被迫不能玩了，他能退到哪里去呢？<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 事实上，无论谷歌是做这个决定的真正原因是什么，在展现给公众的说法上，谷歌有一个失策，谷歌说，他不想再接受敏感内容的审核了。注意，这里说的敏感内容其实不是指情色内容，官方对情色内容从来都不敏感，不光不敏感，估计官员们都已经搞到龟头麻木了。这里所谓的敏感内容只是指不利于政府利益的内容。但是所谓的开放所有审查结果，现实的中国人有多少人在乎呢？这些在正常的国家可以感动国人的理由，在中国看似不太管用。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 中国有两亿网民，谷歌如果问大家是不是想看到不被审核以后的搜索内容，我想应该有两亿减去网络评论员人数都会答应，当然，这就像买菜，你多给人家一点人家总是乐意的。但是如果百度给每个网民十块钱人民币，说只要下载了百度新开发的屏蔽谷歌搜索的浏览器，并且使用我的不光完全遵守而且超额遵守中国“法律法规”的搜索引擎作为唯一指定搜索引擎，你们就可以得到这个钱，估计得有一大半人变节。中国人追求那些危险的普世价值么？中国人追求的，但中国人是顺便追求，追求那些东西在很多人心中的价值未必有追求新开盘的一个楼盘或者追求一点网络游戏中的装备那么高，因为大家的生活压力都这么大，理想都没有，混口饭吃就行了，你跪着吃和站这吃有什么区别呢。谷歌可能高估了自由，真相，公正，叉叉等东西在中国很大一部分网民心目中的价值，这些都没有路上捡到一百块钱实在。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 真的，谷歌还不如说自己老是被中央电视台陷害所以退出更实在一点。谷歌所说的那些理由，无法让这个民族的大部分人民认同和共鸣。一个能吃转基因粮，地沟油菜，三聚氰胺奶，打劣质疫苗针的民族，他们的忍耐力是你所不能想象的高，他们的需求是你所不能想象的低。<br />
