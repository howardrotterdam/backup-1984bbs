													<h2>中华人民共和国民主斗士指南</h2>
						
						
																											<div id="postmessage_214433" class="t_msgfont">最近，刘沙沙的绝食抗议取得丰硕的成果，直接导致了许志永的获释。这个事件对我摸摸党打击很大，因为它证明了我党长期嘲笑沙沙，谩骂民主控的行动是失败的，在操作上倒行逆施，在策略上不得人心。有鉴于此，身为摸摸党主席的我，愧疚极了，同时也化愧疚为力量，深刻反省我党的路线错误，重新思考中华民主的未来。我的决定是，顺应世界潮流，走民主斗争的道路。还有党内的同志提出疑问，为什么沙沙营救了许志永，没有营救张怀阳？我认为，提这种问题本身就是不懂民主。民主最大的特征就是利他主义，先人后己，效应如同隔山打牛，离得越近就越要牺牲，离得越远就越能获救。怀阳离沙沙太近，沙沙的功力没办法发挥到极致，这才导致了悲剧。为了拯救更多的张怀阳，我彻夜不眠，撰写了这部《民主斗士指南》，请各位摸摸党成员放下成见，遵照学习。<br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;声援<br />
&nbsp; &nbsp;&nbsp; &nbsp;每一次声援，都是民主教育。告诉人们，自己多么纯洁、正义、无私。可是每天都有人被抓、被打、被伤害，哪一桩才需要声援呢？很简单，北风在哪里出现，我们就在哪里声援。他喜欢自拍视频，我们甚至可以用视频表示声援，放在他常去的成人网站。总之，我们的声援被北风看见了，就等于给所有人看见了。<br />
&nbsp; &nbsp;&nbsp; &nbsp;每次最重要的是，要强调我们声援的是好人。好人才会受苦，受苦的是好人。要多读圣经，里面有很多典故，如所多玛，有罪就活该遭天谴。还要多读佛经，因为有很多经文，可以显示慈悲，别人遭难我们呻吟。声援要哭。没眼泪，就揉眼睛。眼睛红了，就说心里淌血。最高境界的声援是去广场，拉横幅，竖标语，放风筝。天安门广场，主要是拍照不方便，不然是最理想的，尤其是起风的时候，沙子容易入眼。人越多越好，穿着统一的T恤，V字型手势。第一张眼神要坚定，深情要悲愤，嘴角向下。第二张眼神要坚定，但可以微笑，尽量不露齿。还可以拍些大家聚餐时的照片，大家很轻松，气氛很活跃，表示生活很美好，但要穿插标语牌，例如“HuJia，我们等着和你涮藏羚羊肉”，或者“晓波，你是否记得我们约好去桑拿。”<br />
&nbsp; &nbsp;&nbsp; &nbsp;群体性声援要通知莫之许，因为聚餐不能没人买单。莫之许曾经解释了群体性事件的意义，民主如同聚餐，有些人坐享其成，有些人付出血汗。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;抗议<br />
&nbsp; &nbsp;&nbsp; &nbsp;声援某人，抗议某事。抗议一定要有人声援，可以分成AB两组。A组负责抗议，人不能多，多了就无法树立典型。B组负责声援，人越多越好，可以参考歌迷会的运作，招募义工举着发光的名字牌，酬劳是刘晓波的《美人赠我蒙汗药》。<br />
&nbsp; &nbsp;&nbsp; &nbsp;上街宣传，宣传什么不重要，重要的是上街。首先要把电脑硬盘沉到长江，然后再到黄河边印好传单，最后去珠江找北风经常出没的地方。无论如何，抗议一定要被北风看见。有空的话去一趟拉萨，或者到乌鲁木齐，拍些照片，里面一定要有天真的孩子。民族问题有两种抗议方式，反对恐怖主义，或者反对殖民主义。站在哪一边，要看北风和莫之许站在哪一边，他们的抗议都有聚餐。他们一致最好，如果意见不同，那就看艾未未站在哪边。艾未未抗议搞的是自助餐，伙食不错，有烟肉有咸鱼，就是人太多，每次都吃不饱。而且经常看见某些艺术家男人走来走出，个个长的像马克思，他们的大胡子上沾满了沙拉酱。<br />
&nbsp; &nbsp;&nbsp; &nbsp;在公共场合示威，带上标语牌很容易被人发现，所以要藏好，不能被人看见。最好把标语写在T恤上，穿在里面，外面套一件雨衣。路人经过，就打开给TA看，要很快。打开之前要说，“小妹妹，你看这里面有什么。”静坐静坐，一定要安静，不能发出声音。否则的话，就会让自己的行动失败。要做这个动作――伸出腿，把经过的每一个路人都绊倒。将路人绊倒之后，一定会发生肢体冲突。这样就可以拍照了。鼻青眼肿的照片，是自己为民主献身的强有力证明，要发到网上。有些成人网有SM专版，为了加速传播，也可以发到那边。民主人士为了避开土共监控，经常会到成人论坛谈论国事，就像清末的革命党人经常上青楼一样。<br />
&nbsp; &nbsp;&nbsp; &nbsp;绝食是最极端的，在床上最好，选一些重口味的AV，浣肠、呕吐、吃屎。要学习这种AV里的呻吟声，充满了苦难。要打电话给民主同志汇报进程，“面包都发绿毛了耶”、“电饭煲都生锈了耶”、“我家旁边的快餐店因为我不来都倒闭了耶”。记住，不能打电话给北风莫之许艾未未，因为他们会要求聚餐。要打电话给王丹，他会立刻挂掉电话，亲笔写封信来鼓励。信里这样写道，“小心哦，千万别像吾尔开希那样，绝食的时候去下馆子被狗仔队拍到了。”<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;捐款<br />
&nbsp; &nbsp;&nbsp; &nbsp;纳税光荣，这是公民的基本责任。无论如何要记得纳税，否则就脱离了合法性斗争的轨道。要争当纳税大户，这样我们就有理由要求民主，有自己选举的代表。<br />
&nbsp; &nbsp;&nbsp; &nbsp;捐款的原理也一样，只不过这一回我们扮演政府的角色，而人民还是人民。他们的钱捐给我们，然后我们再纳税给国家。国家拿这些钱养活人民，人民又再捐钱给我们。这样拉动了内需，人民有了温饱，就更加支持民主了。<br />
&nbsp; &nbsp;&nbsp; &nbsp;没有不捐款的民主运动，捐款的数目决定了每一次民主运动的走向。June Fourth事件就是因为捐款数目庞大，所以才走向了海外，否则的话这些人呆在国内，提高了国内的失业率，不利于安定团结。我们和当局作斗争，一定要有苦难，越多的苦难就需要越多的报道，越多的报道就有助于我们从海外拉来捐款。不过，绝对不能承认我们也有工资，月底也会发二斤茶叶，秋天也会猛吃大闸蟹，不然我们苦难的形象就会崩塌。我们需要宣传，可以承诺别人有稿费，但是一定不能支付，不然他们会以为我们是牟利机构。<br />
