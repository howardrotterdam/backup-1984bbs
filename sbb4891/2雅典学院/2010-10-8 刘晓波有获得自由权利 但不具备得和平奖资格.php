&nbsp; &nbsp;[按]08宪章是张祖桦起草的。刘晓波后来才看到08宪章。中共很离奇地放过多少年一直是军师和背后操纵者、组织策划许多次行动的张祖桦，关押刘晓波，并且把宪章的一切，都归到刘晓头上，然后，中共和装成反对派的五毛唱双簧大造声势，这不仅不符合常理，而且显然是别有目的。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;　　　　　　　　　――网路文摘编者2010-10-4日<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;有消息说，刘晓波被提名参评诺贝尔和平奖，因此，就有可能获得诺贝尔和平奖。我却认为，综合他各种表现，离这个荣誉尚有一定距离。如果刘晓波获奖，对于中国知识界乃至中华国民的人格重建，对于中国民主化进程的未来，都将带来负面的影响。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;第一，刘晓波的行为，反复无常,显示出其道德高度不高。其反复无常行为的中心，是精确地惦量自己的成名与否，获利与否。他参加了六四，又向当局忏悔了六四；他向当局忏悔了参加六四，又对自己的忏悔行为表示了忏悔。他曾向天安门母亲、在天安门事件中失去爱子的丁子霖教授表示过同情，尔后，他又把在天安门事件中牺牲的爱国同胞和民主战士说得一文不值，这也就否定了丁子霖爱子牺牲的价值。他对六四运动和六四牺牲价值的否定，引起了丁子霖夫妇的反感。后来，他为求得丁的好感，又表示了忏悔。刘晓波变来变去，悔来悔去，不知道他究竟信仰什么，不知道哪一个刘晓波才是真实的刘晓波。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;他的多变，有一个轴心，或者说，他的行为的多变，有一个价值取向，变中有不变。那就是他权衡了自己的利害得失。八九民运，总体上是个伟大的运动，足以惊天地，泣鬼神。历史愈长久，将愈益显示出它的精神的伟大和影响的深远。刘晓波参加八九民运，他曾自我表述过自己的动机：“不管别人如何议论，我都坚信，“八九抗议运动”之机太值得投了，能够投上此机，确乎上帝有眼，赐福于我，即使被指责为政治投机者，也心地坦然，无怨无悔。”（《末日》，第七十四页）他的自白，活脱脱地表现出一个投机者的心态。刘晓波就如一个小商人买股票，将自己的全部家当都投了进去。一次投得输了，就埋怨自己不已。过一阵，看这个股涨了，又对自己昨天的埋怨怨恨不已。正是这种投机心理，使他的对六四运动的表现，有时候热得发烧，有时候又冷得发抖。一会儿他把自己当作救世主，盖世英雄，一会儿则又低眉顺眼，丧魂落魄，自我否定，甚至否定一切。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;果然，他入狱了，在狱中受到一点压力，这本来是很自然的，是应该有这个心理准备的。但是，他又作了忏悔，对六四作了全盘否定，写了认罪书。认罪书就是认罪书，在那种情况下，也还是可以谅解。但是，他却要把写认罪书当成一件伟大的救国救民崇高行为来歌颂。他自述说：“我是民主斗士、中国的精英分子，肩负着民族的重任、人民的期望，我的思想、我的形象、我的声音、我的一举手一投足都将关系到中国的未来。如果我的声音-沉寂在狱中，岂不是对中华民族不负责任？岂不是国家的一大损失吗？如此宝贵的财富怎么能轻易地放弃而不惜之如生命、如自由呢？决不能！为中国的民主事业计，为中华民族的前途想，我一定要好好活着、精心地保护自己、用一切手段争取自由。而我一旦自由了，就能够继续发挥作用，完成未竟的事业。”就这样，他把自己的忏悔行为或认罪行为与民族、国家的前途联系起来，似乎真的感到自己的生死存亡有着极为重大的意义，把自己的软弱和自我否定，当成了伟大和神圣的行为。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;这是一种赤裸裸的投机心理，也是一种利己心理，赌博心理。这种心理支配下，他随时可以为自己的一切行为，找得到非常充足的理由。他向当局的忏悔，果然找到了非常堂皇的理由。刘晓波对自己的自我否定，对六四运动的否定，说成是为了中华民族的社稷江山，为了千秋万代的民主大业。他越是这样打扮，就越是显得政治道德的缺乏和行为的丑陋。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;后来他自由了。出狱以后，他开始对自己在狱中的“忏悔”行为，进行了新的否定，开始了新的忏悔。因为他明白，自己的忏悔行为，毕竟不能说是光辉的，它损害了自己的形象。他想要弥补“忏悔认罪”带来的损失，就又作了新的投机。有一次，他在诗人江河纽约的家中，明白无误地表示：他在狱中写了《悔罪书》，栽了，这次要通过忏悔，再捞回来。这一次想捞回来的办法是写了一本书，即《末日幸存者的独白》。对六四运动、六四参加者作了全面的否定。不断变化的刘晓波多变易变的行为中，有一个轴心没有变，那就是利益的支点。正如著名作家郑义所说的，刘晓波“一举手一投足，都在惦量名利得失。”诺贝尔和平奖重视人的道德操守，重视一个人信仰真诚，如果诺贝尔和平奖让人这样一种人格的人，或者让这样一种人参评，显然是极不相配的，会影响这个奖的神圣和光辉。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;第二，刘晓波对于六四运动中成千上万受难者（有的死亡，有的坐牢，有的处分，有的失业，有的流亡海外，等等）持完全否定的态度，对这个运动作了全盘否定，这起到了镇压者所不能起的作用。客观上是帮助镇压者论证了血腥镇压的合理性。这样做，完全是对历史的否定，是对百万民主运动参与者的贬低，也是对镇压者的帮助。这是历史不能原谅的，也是千百万运动参加者所不能原谅的。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;这场运动，对于中国未来发展具有深远的影响，它体现了中华民族热爱自由、向往民主的精神；体现了中国国民性中敢于牺牲、关心国事、危难相助的品质。刘晓波对六四精神的否定和污蔑，使他站到了这场运动的对立面，站到了正义的对立面，站到了历史潮流的对立面。他对六四的否定，是千百万六四的参加者、上千个民主运动的殉难者，和无数个因参加六四运动而蒙受各种打击和迫害的人们所无法原谅的。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;他在出狱出版的著作《末日幸存者的独白》中，对这场运动，作了全盘的歪曲和否定。他写道：“各种人，怀着不同的动机，说着各异的谎言，此乃机不可失。政治性谎言、道德性谎言、求生性谎言、官方谎言、精英谎言、民间谎言……。‘六四被谎言打扮得一塌糊涂，也被谎言强奸得人老珠黄。。”（《末日》十二页）“左右这次运动的重要力量之一就是层出不穷的谣言。谣言的力量绝不次于任何一个学生领袖和知识精英的宣言、演讲。”（《末日》，第一八七页）就这样，当年他慷慨激昂地投入过的、演讲过的、甚至绝食过的千百万人参加的声势巨大的运动，变成了谎言的运动，谣言泛滥的运动。他否定了自己，也否定了整个运动，否定了投入这场运动的千百万人的真诚，否定了被子弹和坦克扼杀了生命的青年。这种否定，也理所当然地否定了他作为诺贝尔奖后选人的资格。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;任何伟大的运动，难免会有各种人物登场，戴上各种面具。这在一个大运动中是难免的。不能因为长江有泥沙，就将长江全部否定。不能因为太阳有黑子，就说太阳全是黑的。不能因为婴儿身上有血，就将婴儿掷掉。而刘晓波却将六四全盘否定了。他对六四运动和六四参加者的否定，给千百万参加者带来极大的痛苦。诗人江河后来读了向刘晓波买的这本书，当读到对这场运动的全盘否定，只感到胃受不了了，想要呕吐。刘晓波在书中对运动和运动参加者的污蔑和否定，带给一个历史的亲历者极大的痛苦：不仅带来了心理痛苦，而且带来了生理痛苦。他的这种对六四的全盘否定，也给丁子霖夫妇带来极大的精神痛苦。一个让千万个抱着热血参加伟大运动的人们感到痛苦的人，如果他得到诺贝尔和平奖，那是非常不合适的。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;简而言之，刘晓波缺乏作为诺贝尔奖后选人所必须具备的道德高度和信仰真诚。一个信仰易变、一个不断悔罪、忏悔、自我否定和贬低他人的人，一个不断地随着自己处境的变迁和利益的权衡而像变形金刚一样变来变去的人，如果作为诺贝尔奖的后选人或获奖者，那是对以往一切崇高的和平奖得主的一种矮化，会使诺贝尔和平奖评奖委员会的声誉蒙尘，更会对中国真正的民主精神的一种打击。而且，他对六四运动和六四参加者的丑化，客观上为邓小平的血腥镇压开脱罪责，为专制主义帮了忙。如果授与这样一类人格的人，是对中国人道德重建设置错误的样板和错误的导向，这实在是非常可悲和可叹的事情。据此，我认为刘晓波是不适宜获奖的，不具备获奖的资格。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;但是，我还是要说，刘晓波曾经是六四的参与者，他自己否定了自己，但是历史不会否定他的参与和曾经表现过的真诚。我还要说，他起草零八宪章是无罪的，这是他的权利。某些人以他参与起草这个文件而指控他颠覆国家政权罪的罪名是不能成立的。这是对法律精神的践踏。中国领导人曾经表示要尊重宪法，尊重法律，表示要以法治国，要给宪法以尊严。那么，从宪法的立场看，刘晓波无罪，应该给他以自由。对刘晓波的定罪，能称得上是以法治国吗？但是，刘晓波无罪与他有没有资格得到诺贝尔奖，则两件不同的事情。因此，我不认可刘晓波诺贝尔和平奖的参评资格，但是，却坚定地支持给予刘晓波自由说话的权利。这个权利，是联合国人权宪章所赋予的，是中华人民共和国宪法所赋予的，也是每一个人生而有之的。<br />
（2010/10/08 发表）</div>

							
							
							
							
