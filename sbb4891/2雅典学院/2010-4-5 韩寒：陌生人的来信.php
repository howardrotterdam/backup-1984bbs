													<h2>韩寒：陌生人的来信</h2>
						
						
																											<div id="postmessage_548618" class="t_msgfont">转载：<a href="http://blog.sina.com.cn/twocold" target="_blank">韩寒新浪博客</a>，2010.4.5，<a href="http://is.gd/beHei" target="_blank">http://is.gd/beHei</a><br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 因为以前有一个报纸写过去我老家自驾游的攻略，所以有一些来访者也曾找到过那里，送来一些小礼物或者拍一些照片，我都会看到，但是最近看到放在老家的一些来信，让我读后觉得非常无奈困苦。<br />
 <br />
&nbsp; &nbsp;&nbsp; &nbsp; 我们国家有一个部门，叫做信访办。古代老百姓受了当地官员的冤屈以后，就会上京告御状，运气好的还能拦到当官的轿子，运气最好的能遇见微服私访的皇帝，这些小概率事件乃是支持整个社会对公平正义向往的精神支柱。到了现代，领导们都换上了好车，不能再用拦轿子的方式自杀了，更大的领导由于电视曝光率很高，也不能微服私访了，就算下乡，最多也是去一些当地领导特地安排的影视基地，和一些农民艺术家们进行表演，但那都是影帝们在飙戏，和老百姓的关系不大，信访办是很多遭受了不公正待遇的人们唯一的出路。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 当然，很明显，他们想的太天真了，在一个司法不独立的国家，你怎么能指望突然会有一个政府部门为你出头呢，一个小朋友打你一下，他妈妈骂你一句，他爸爸还揍你一拳，你去他爷爷那里举报他儿子和孙子，你明显是还欠踹你一脚。虽然他们那挑高三十米的办公楼大堂里可能挂着诸如为你服务等文字，但人家是把这个当书法作品在欣赏，你怎么能把这个误会成人家的行动纲领呢。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 于是，在明白了上访乃是自投罗网主动进入黑名单以后，越来越多的人碰壁以后想到了媒体，追求公正就变成了和追求女人一样，只要搞大了，这事就成了。毫无疑问，中国的媒体人和中国的公务员是有着本质区别的，每个职业都有每个职业的追求和素养，媒体人基本上是有自己的媒体理想和新闻追求的，虽然他们也不能违反每天下发的禁令，但是只要在他们的能力范围以内，他们都是嫉恶如仇的。再比如车手，职业追求就是开的快，演员，职业追求就是演的好，但是我始终无法知道公务员们的职业追求是什么，是办公务么。也许他们的职业追求就是好吃好喝，游手好闲，察言观色，见风使舵，最终顺利变成官员，可以有权有势有灰色收入。恰恰因为他们没有正当的职业追求，所以他们没有职业素养。基本上，上访者在他们眼里都是没有大局观的刁民。<br />
 <br />
&nbsp; &nbsp;&nbsp; &nbsp; 很多受到了不公正待遇的朋友们把我当作了媒体，在杂志的稿件和我每天收到的信中，有不少都是希望我主持正义，帮他们写一写，让他们的遭遇引起媒体的关注。我每封信都认真的看了，但是我非常的无奈，这些事情在你们家庭的身上是个沉重的负担，但是对于新闻媒体，这已经失去了新闻价值，我相信就算我为你写一些什么，也不会有传统媒体的关注。而一件事情的解决，往往需要很多传统媒体的帮助才可以，领导才会出来装腔作势的急群众所急，想群众所想。信中最多的是某个小区交房质量很差，某个小区边上是个垃圾站或者变电站，还有最多的就是我被强拆了。你如果被强拆了，那不是新闻，那是生活。如果你本人没有烧焦，还能收发邮件，全家老小全部健全，那就是幸福生活，你应该感谢国家。<br />
 <br />
&nbsp; &nbsp;&nbsp; &nbsp; 最惨的一封来信来自于一个外地来的朋友，所有的材料都很全，内容是一家人被强拆了，还有人受伤，家里的大部分面积被算成了违章建筑，他们去北京上访，结果材料被退回到省，省退回到市，市退回到县，县退回到村，然后每逢国家重大节假日，他们一家都被联防队监控起来，以防破坏和谐气氛。最后他们告到了法院，法院居然受理了。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 天哪，法院居然受理了，法院难道不是政府的一个服务机构么，怎么会受理此案呢？我迫不及待了翻到了下一页。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 在这一页里，法院居然很快判决了，判决的结果是原本政府要赔受害者二十万的，现在政府只需要赔十万。<br />
 <br />
&nbsp; &nbsp;&nbsp; &nbsp; 在我收到的这些信件中，我并不能公布的另外一个重要原因是我并没有核实过，但是我个人又没有能力去核实这些。虽然我相信大部分都是真实的，甚至全部都是真实的，最多就是在艺术上多写了一些对自己有利的内容，但是对事情的大局上并无影响，操蛋的肯定是对方。对于这些需要帮助的信件，我觉得自己非常的无力。当然，他们并不是希望我能够为他们带来曙光，他们只是在不停的向所有他们能想到的渠道尝试。<br />
 <br />
&nbsp; &nbsp;&nbsp; &nbsp; 当然，真正苦难深重的人已经未必能够申诉，对于在正在申诉自己苦难的人，他们始终没有一个申诉的途径，他们曾经向干部申诉，后来发现好像除了干部以外也没其他什么人欺负他们，于是他们向组织申诉，后来发现组织是由大大小小的干部组成，然后他们找到了信访办去登记了一下自己，以便于公安机关监控，最后他们到法院去缴纳了诉讼费，这条路上绕来绕去都是敌军，于是他们另辟蹊径，他们找到了媒体，但是发现苦难者太多，自己的苦难不够深重，没有达到新闻的级别，然后他们找到了网络，但是发现倒霉蛋太多，他们的倒霉不够独特，没有达到被顶的级别，然后，他们怎么办？</div>

							
							
							
							
