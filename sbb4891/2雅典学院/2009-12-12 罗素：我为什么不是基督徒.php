仰基督教时，就有宗教裁判，和宗教裁判的酷刑，数百万不幸的女子被当作女巫活 <br />
活烧死，各种残酷的人性，假藉宗教之名而迫害人类。 <br />
<br />
环顾世上，我们发现仁慈情操的每一次进步，刑法的每一次改良，减轻战祸的每一 <br />
个步骤，有色人种待遇的每一次改善，奴隶制度的每一次缓和，世上一切道德进步 <br />
的过程，都一致遭到有组织的教会反对。我十分慎重地说一句话：基督教以其教会 <br />
组织的形态，一向是，而且今天依然是道德进步的主敌。 <br />
<br />
<br />
<br />
教会阻碍进步的方式 <br />
<br />
你们也许认为我说教会今天依然是道德进步的主敌这句话言过其实。我不以为自己 <br />
有什么言过其实的地方。举一件事实为例，希望诸位能忍受。这是不愉快的事实， <br />
但是教会使人不得不提到不愉快的事实。假设现在有一位没有经验的女子和一个梅 <br />
毒患者结婚，天主教会会说：&quot;婚姻是不能解除的圣仪，你们必须忍受独身或共同 <br />
生活。假如你们选择共同生活，你们也不能用节育的方法来防止受梅毒病菌侵害的 <br />
婴儿出生。&quot;任何人只要天生的同情心尚未完全泯灭，道德天性对于别人的苦难还 <br />
不能置若罔闻，就不能说这种作法是对的。 <br />
<br />
这只是一个例子。目前，教会以所谓道德为名，用种种方式使人类遭受不应得的、 <br />
不必要的痛苦，而且阻碍进步，阻碍减轻世上苦难的一切改良方法。它选定一套狭 <br />
窄的行为法则，称之为道德，这套法则对于人类的幸福没有丝毫帮助。每当有人提 <br />
议作某些事情以促进人类幸福时，教会就觉得无此必要。&quot;人类的幸福与道德有什 <br />
么关系呢？道德的目的并不是要使人类幸福。&quot; <br />
<br />
<br />
<br />
恐惧 DD 宗教的基础 <br />
<br />
我觉得宗教信仰主要是基于恐惧。一部分是由于对未知的恐惧，一部分如我方才所 <br />
说，是渴望有位长兄之类的人物，在一切麻烦和争执中支持自己。恐惧是宗教的基 <br />
础 DD 对神秘的恐惧、对失败的恐惧、对死亡的恐惧，恐惧是残酷之母。残酷与 <br />
宗教永远携手并进，这是再自然不过的情形，因为恐惧是此二者的基础。今天，我 <br />
们刚开始了解事物，刚开始藉科学之助控制事物，科学一直在一步一步地向基督教 <br />
，向教会，向一切古老的格言进军。科学可以帮助我们克服这个困扰人类世世代代 <br />
的卑怯的恐惧，科学可以教导我们，我们的心也可以教导我们，我们不必再四处去 <br />
寻求幻想中的支持，不必再捏造并无其人的盟友，我们宁可脚踏实地，凭自己的努 <br />
力把世界建设成一个比教会构筑的今生与来世都更适合人类居住的地方。 <br />
<br />
<br />
<br />
我们应该怎么作 <br />
<br />
我们应该站立起来，公平地正视这个世界DD善的事实、恶的事实、美的、丑的， <br />
看清世界的真面目，不必畏惧。我们应该以才智征服世界，不可奴隶般地慑服于恐 <br />
惧。上帝的观念全部来自古东方的专制主义，不值自由思想者一顾。常常有人在教 <br />
堂中贬抑自己，说自己是可憎的罪人，这种事是何等的可鄙，不值得有自尊心的人 <br />
类去作。我们应当站立起来，坦率地正视世界，我们应当尽我们的能力去善用这个 <br />
世界。假如这个世界不像我们希望的那么好，它毕竟还是可以渐渐好转。一个好的 <br />
世界需要知识、仁爱和勇气，不需要惋惜过去，不需要让古代无知者之言束缚。它 <br />
需要无所畏惧的思想和自由的才智，它需要对未来的憧憬，而不是对既往的追忆。 <br />
我们相信，凭我们的才智，我们所创造的未来，必定远胜于过去。</div>

							
							
							
							
