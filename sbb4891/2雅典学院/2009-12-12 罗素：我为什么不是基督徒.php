													<h2>罗素：我为什么不是基督徒</h2>
						
						
																											<div id="postmessage_369682" class="t_msgfont">这是一九二七年三月六日罗素在巴特斯市镇大厅发表的演说辞。此次演说由国际世 <br />
俗协会的南伦敦分会主办。 <br />
<br />
<br />
今天晚上我所要讲的题目就是方才主席所说的&quot;我为什么不是基督徒&quot;。也许我们最 <br />
好先弄清楚&quot;基督徒&quot;的意义。由于滥用的缘故，现在&quot;基督徒&quot;的意义相当含糊。有 <br />
些人仅以它来代表一个企图以善良的方式过活的人。这样解释的话，我想各个教派 <br />
之中都有基督徒存在。但是我认为这个解释并不恰当，这等于暗示所有的非基督徒 <br />
DD 包括佛教徒、儒者、回教徒等等 DD 都不打算以善良的方式生活。我所要 <br />
说的基督徒并不代表任何尝试以个人的智慧去过端正的生活的人。我认为一个必须 <br />
有相当明确的信仰，才能自称为基督徒。现在这个名词所代表的意义，已不再像圣 <br />
奥古斯丁和阿奎那的时代那样单纯。在那时，假如一个人自称是基督徒，人人都懂 <br />
得他的意思：他接受一套明确的教条制度，他以完整的信念全力信仰这些教条中的 <br />
每一个音节。 <br />
<br />
<br />
<br />
什么叫做基督徒？ <br />
<br />
现在的情况不同了，我们为基督教下定义时，不能不稍微含糊一点。我个人认为， <br />
每一位自称基督徒的人，都必须具备这两个条件。第一个条件是非常武断的，就是 <br />
，必须信仰上帝和不朽。不信仰上帝和不朽的，就不能自称是基督徒。第二，根据 <br />
&quot;基督徒&quot;这个名词本身所代表的意思，必须对基督其人有某种信仰。回教徒也信仰 <br />
上帝和不朽，但是他们不自称基督徒。假如不认为基督是神，最低限度应该相信基 <br />
督是至善至慧的人，不然的话，就没有资格自称基督徒。当然，就另一个说法，像 <br />
怀台克所编的&quot;年鉴&quot;，和把世界人口区分为基督徒、回教徒、佛教徒、和拜偶像者 <br />
等等的地理书，我们全是基督徒。这些地理书把我们全体归类为基督徒，这纯粹只 <br />
有地理学上的意义，我们可不予理会。所以我现在要告诉你们我为什么不是基督徒 <br />
，就要同时说明两件事：第一，我为什么不相信上帝和不朽。第二，我为什么不认 <br />
为基督是至善至慧的人，虽然我对他的道德评价颇高。 <br />
<br />
由于不信仰者在过去不断努力的成果，我今天才能给基督教下这样一个广泛的定义 <br />
。如我方才所说，古时候&quot;基督教&quot;所代表的意义，要比现在纯粹得多。从前的基督 <br />
徒还必须相信地狱之说。一直到不久以前，永久的地狱之火是基督教信仰里的一个 <br />
重要项目。你们都知道，地狱的说法后来被枢密院废除了。肯特堡大主教和约克郡 <br />
大主教不承认这项决定。但是在我们的国家里，我们的信仰全凭国会的法案裁决， <br />
枢密院的职权凌越大主教之上，因此基督徒不必再信仰地狱之说。我也不必坚持信 <br />
仰地狱之说是基督徒的条件之一。 <br />
<br />
<br />
<br />
上帝的存在 <br />
<br />
我们先来讨论上帝的存在与否：这是一个严重的大问题，若要予以彻底的处理，就 <br />
必须请诸位坐在这里等待来世。所以假如我的处理方式不够具体的话，请诸位务必 <br />
原谅。你们都知道天主教会有一个信条：上帝的存在是不证自明的。这句话听起来 <br />
实在奇怪，但这是他们的教条之一。他们不得不提出这个信条，因为曾有一段时期 <br />
，自由思想者养成一种可疑的习惯，他们喜欢一面提出各种论证，说明纯理智否认 <br />
上帝的存在，一面又说，就信心的立场来说，他们当然明白上帝的确存在。他们把 <br />
这些论证和理由刻意铺叙得十分详尽，使天主教会觉得有加以禁止的必要，因此主 <br />
张上帝的存在不证自明，而一面却又建构他们所谓的论证来加以证明。他们的论证 <br />
有很多种，我这里只提出几种来讨论。 <br />
<br />
<br />
<br />
第一因论证 <br />
<br />
最简单易解的就是第一因论证。第一因论证主张万事万物都有因，循着因因的连锁 <br />
一步步往上追溯，就得到第一个因。我们把上帝之名给予第一个因。这个论证目前 <br />
已经没有多少份量了。首先，因的性质已经不同了。在哲学家与科学家的反复研究 <br />
之下，因已失去了以往的活力。即使不提这个，你们也知道第一因论证本身就是无 <br />
效的。当我还年轻，我的心中还在严肃地辩论这些问题时，我曾有一度接受第一因 <br />
论证。直到十八岁时，有一天我读了J・S・穆勒的自传，读到这样一个句子：&quot;我 <br />
父亲告诉我'我是谁创造的？'这个问题是没有答案的，因为这个问题牵连更进一步 <br />
的问题'上帝是谁创造的？'&quot;这个简单的句子使我猛然悟出第一因论证的谬误。假 <br />
如万事万物都必须有因，那么上帝也必须有因，假如无因的事物有存在的可能，则 <br />
世界和上帝都可以无因，所以第一因论证完全无效。这与印度教徒的见解有异曲同 <br />
工之妙。印度教徒认为世界被一头象顶在背上，这头象立在一只乌龟背上。假如有 <br />
人问起：&quot;那只乌龟又如何呢？&quot;印度人就说：&quot;换个话题吧！&quot;第一因论证实在不比 <br />
这个高明。我们没有理由认为世界的出现非有原因不可，也没有理由认为世界不是 <br />
一向就存在的。假定世界有一个开端是毫无道理的。万事万物都必须有开端的想法 <br />
，实在是由于想象力贫乏所致。因此我不想在第一因论证再浪费时间了。 <br />
<br />
<br />
<br />
自然律论证 <br />
<br />
接下来我想谈谈常见的自然律论证。这是十八世纪中最受欢迎的论证，特别受欢迎 <br />
的原因是因为有牛顿和他的宇宙起源论助阵。人们观察出行星依据万有引力定律环 <br />
绕太阳转动，认为因为上帝命令行星以这种方式转动，所以行星就照办。这当然是 <br />
一个再便当不过的解释，为人们省去了解释万有引力定律的麻烦。目前我们解释万 <br />
有引力的方式，是爱因斯坦提出的。我不打算在此说明爱因斯坦如何解释万有引力 <br />
定律，以免花费太多的时间。总而言之，牛顿系统中的自然律今天已经不能成立了 <br />
。牛顿认为由于某种不可解的原因，自然界中的一切以一致的方式推移。现在我们 <br />
才发现，许多我们一向当作自然律的东西，其实是人为的习惯。要知道，即使在星 <br />
空中最遥远的角落，一码还是等于三尺，这是再明显不过的事实，但不能叫做自然 <br />
律。许多所谓的自然律都是这一类的。从另一方面来说，我们研究原子的实际动态 <br />
，到最后会发现原子的运动并非依据任何所谓的定律。我们计算出来的定律，只是 <br />
