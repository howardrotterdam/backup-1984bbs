-------------------------------------------------------------------------------------<br />
回应罗素的《我为什么不是基督徒》<br />
<br />
小灶<br />
<br />
序<br />
<br />
我第一次接触到罗素这篇演讲大约是1997年南开大学的BBS站上。<i> 当时有网友把这篇演讲的中译文按顺序贴出来，造成很大影响，于是我没多计划就写回应。但后来因为时间关系，只写到传统关于上帝存在的证明。这一停就是几年。这期间虽不时有朋友催促我把它完成，但因为我曾看到过好几篇其他对此文的回应，[ii] 所以没有很着急。今年暑假稍微有了点时间，就希望把它完成。<br />
<br />
现有的几篇回应中，Smith和Bahnsen基本采用相同的思路，侧重于针对罗素这个人进行整体性的考量；Geisler则选择主要论点加以反驳。不过它们都是英文，对中文读者影响不大。中文有章力生先生的回应，但一来时代比较久远，二来其辩论方式基本采用直接引圣经，有基督徒自说自话之嫌。另外，相较于西方知识界，中国知识分子所受的影响中，基督教的太少，而罗素的又太多，鉴于这样的情况，我相信再写一篇中文回应是合适的。<br />
<br />
数位研究罗素的学者都发现，罗素常常是一个非常情绪化的人，[iii] ――虽然他作为分析哲学的创始人之一，在研究数理逻辑问题时又很清晰。这篇“我为什么不是基督徒”的演讲，通常认为是他处于情绪化状态的代表，所以论证的力量主要来源于修辞所产生的感染力和煽动性，而不是逻辑和哲学思辩，因此该文在学术界也并没有多大份量。[iv] 我当初回应时也用了很多情绪化的词句，颇有来而不往非礼也的气势。但现在时过境迁，已经没有那样的意气了。所以这篇文章实际上被重新整理了一次，虽然在与原来重叠的部分上，要点并没有大变化。下面仍按罗素发表这篇演讲时的分段来回应，最后作一简短总结。<br />
<br />
什么是基督徒？&nbsp;&nbsp;我想罗素在这里的思路是：要说明“为什么我不是基督徒”，当然要先说明我对“什么是基督徒”的看法。而要反驳他的话，自然就会质问：「罗素所理解的基督徒」是否就是真的基督徒？换句话说，罗素完全可能是在堂吉柯德式地大战风车。这个问题深入问下去可以牵涉出很多哲学上、或认识论上的问题，而显然罗素在这里没有兴趣作这些思考。另外，从哲学史上来说，罗素自己在认识论和形而上学上的观点也相当混乱，贡献远不如他在数理逻辑上的工作。[v] 因此，我原来的回应基本上也以此为把柄来反驳他，即认为他并没有了解「基督徒」的真正涵义。这很容易做到，因为罗素把「基督徒」教条式的简单约化为：①相信上帝和永生；②对耶稣表示相当崇敬的人。但这样一来，罗素不仅完全没有抓住「救赎」在基督教信仰中的中心地位，而且就教会传统上对「信仰（faith）」一词的反思来说，他也不得要领，因为他只是将之等同于理智上的认同。不过，从辩论，特别是单从反驳他的技术角度来说，除非他的理解完全跑出「基督徒」一词所涵盖的正确范围以外，我们仍然可以就他提出的这两条来辩论。因为真正的基督徒当然相信上帝和永生，――虽然我们的信仰内容不仅只此，况且罗素对上帝和永生的理解也不一定符合基督徒的理解――；当然也对耶稣表示相当的崇敬，――当然我们的“崇敬”是以之为神来敬拜。另外顺便也指出，罗素开篇为煽动对基督徒的仇恨，使用了相当情绪化的修辞。这篇演讲充斥着类似的春秋笔法，下面除非必要，就不一一指出了。<br />
<br />
关于上帝存在的论证&nbsp;&nbsp;任何在这个问题上进行过认真思考的思想家都无一例外地承认，能够被人的理智在逻辑实证的意义上完全证明的上帝，一定不是在信仰意义上的上帝。其实罗素自己在清醒的时候也承认这一点。[vi] 然而现在他却将天主教的这种立场（也是新教在这类问题上的立场）归因于教会在理智上的无能，实在让人对他的春秋笔法扼腕。不过Smith和Bahnsen（师从范泰尔）基于类似的理由拒绝在这些问题上回应罗素，我也不太满意。因为「不能在逻辑实证意义上证明」不等于「不能说明其合理性」。在这一点上我比较倾向于Geisler的做法。固然，「合理性」这个词本身在基督徒和非基督徒看来，可能都有不同的涵义；但既然范泰尔也承认，非基督徒也不可能在上帝可以触动和改变的范围之外，那么，我们还是可能按照恰当的方式来论证我们信仰的合理性。<br />
<br />
第一因的论证&nbsp;&nbsp;罗素自作聪明地以为他十八岁就能推翻传统上关于上帝存在的宇宙论论证（Cosmological Argument），在Brightman看来，这却令人发噱了。[vii] 因为其谬误相当简单，在于混淆了上帝与世界，――按照基督教信仰的理解，这两者是完全不同的两件事――，属于所谓“范畴上的错误（categorical fault）”。因为上帝是创造者，而世界上其他一切都是被造的，是偶发之物（contingent being）；因此，一切偶发之物需要一个起因，不等于上帝需要一个起因。我原来举的一个例子，虽然尖锐点，倒颇能说明问题：我们不能因为罗素扎了一个稻草人，于是根据稻草人的脑袋里面都是稻草，就说罗素的脑袋里面也都是稻草。这叫做创造者和被造者“在范畴上的差别（categorical difference）”。<br />
<br />
自然律的论证&nbsp;&nbsp;这是所谓关于上帝存在的目的论论证（teleological argument）的一种。罗素在这里有两个子论点。第一点，往好里说，是他认为我们应该把“约定性的（prescriptive）”律和“描述性的（descriptive）” 律加以区分；往坏里说，则是他认为，因为科学家所能给我们的自然律都是描述性的，所以自然界实际并不存在任何本质上的规律。但无论怎么说，罗素靠此点并不能驳倒自然律的论证，因为自然律论证的重点不在于科学家以何种具体的方式来描述，而在于描述本身的可能性，或者说，自然界本身是否有秩序存在，以至于科学家可以对它进行描述。否认了这一点，实际上就否认了科学的合法性。而这对无比推崇科学的罗素来说，无异于自打嘴巴。<br />
<br />
接下来的第二点，如果我们接受约定性和描述性的区别的话，罗素认为接受上帝作为自然律的约定者也有困难，因为上帝制定规律的原因或者：①不在自己以外；②在自己以外。一般来说，对①的批评在于，这样一来上帝就任意了（arbitrary）。但明显罗素这里没有采纳这种批评，而与其第一点混淆了，即他认为规律有例外。但是，如果我们承认科学家所做的仅是描述性的工作的话，那么出现例外毫不奇怪。因为问题的关键仍在于是否有可描述的东西――无论你描述得怎样――，而不在于如何描述。我想实际比较可能的情况应该是罗素作为有相当造诣的哲学家，原本知道这种对上帝任意性的批评；而他在这里提出这一点，原也是要以此来反驳。但老先生当时正处于亢奋状态，思维有点混乱，结果就把第一点和第二点搅在一起了。对②，罗素认为这样一来，上帝就非真正的终极了，――这么说当然对。因此，对基督徒来说，上帝制定自然律的原则当然不在自己以外。但这并不任意，因为基督徒所信仰的上帝并不是一个唯意志论的怪物，而是有智慧、公义、圣洁等一系列具体的属性。因此，上帝一切的作为都与自己的本性相合。“上帝不能背乎自己。”这样，我们就既不能说上帝做事的准则在上帝以外（以至于上帝不再是终极），也不能说上帝做事毫无根据（以至于任意）。<br />
<br />
设计论论证&nbsp;&nbsp;这是目的论论证的另外一种形式。[viii] 粗略来说，达尔文的生物进化论本意要反驳设计论，因此罗素在这里也以此为论点。Geisler认为罗素在这里犯了一个经典的逻辑错误，[ix] 但我倒觉得Geisler把他看高了，用现代无神进化论的看法取代了罗素的看法。就现代的进化论和设计论之争来说，若没有引入“变异（mutation）”的观念，进化论本质上就会蜕化成循环论证（tautology）。从纯粹哲学思辨的角度来说，现在的问题是这样的：我们看到生物都与环境适应得很好。那么如何解释这种适应性的出现呢？设计论者说，这是因为设计者的设计；而进化论者说，这是因为变异和选择的结果。Geisler认为，除非事先就假设了这两种解释是非此即彼的，否则两者并不一定冲突，因为从纯粹逻辑的角度来看，二者没有不可调和的矛盾。就好象要解释我为什么现在不饿，那么原因既可能是因为我五分钟前刚吃了东西，也可能是因为我午饭吃得比较多，也可能两个因素都有，更也可能是我五分钟前吃的就是午饭！现代辩论的双方的确常常在此点上混淆不清。但罗素只用了一个意思很含混的“适应性（adaptation）”一词，很难让人相信他有现代进化论者那样的了解。<br />
<br />
这从罗素对设计论的攻击也可以看出来。罗素在这里要讲的大约是所谓邪恶的问题。不过就技术上来说，他犯了一个很低级的错误，因为具体到生物进化论来说，邪恶问题具体体现在所谓“无用”或“有害”的生物器官或组织上，而不是社会现象中的邪恶。社会中的邪恶现象实际上正好带入基督教的堕落和救赎的观念。而堕落也是基督教对邪恶问题的一个很重要的回答。不过，就纯粹逻辑的角度来说，正如Nash所指出的那样，邪恶问题（包括在生物上的应用）在逻辑上并不必然给基督教的上帝观带来困难。[x] 但与上面类似地，我怀疑罗素在这问题上深入思考的兴趣，因为他马上讨论的是逻辑实证主义式的机械宿命论的世界观，虽然这种世界观与他的伦理学完全相左，正如他在清醒时也承认的一样。[xi]<br />
<br />
道德论论证&nbsp;&nbsp;罗素对道德论论证的反驳实际上与他对自然律论证的反驳一样，所以对他的回答也与前面一样。上帝本为善，所以当上帝说一物为善时，k既不必诉诸于外在的标准，也不必流于任意。总的来说，罗素对上帝的理解还是局限于抽象的理性主义圈子，对基督徒所信靠、圣经中所启示的上帝并不认识。<br />
<br />
补偿公义的论证&nbsp;&nbsp;严格说来，传统上关于上帝存在的论证并不包括一种所谓“补偿公义的论证”。这种论证就罗素所描述的形式来看，也不太有说服力，因为它以「公义已经存在」为更根本的前提。但这个前提显然与通常谈到上帝时，自然就以上帝为终极的观念相左。虽然如此，罗素显然在这里没有真正做逻辑思辨的兴趣，而在于迎合听众对科学的狂热，所以他的反驳也经不起逻辑的推敲。罗素这里隐含的前提包括归纳的合理性和合法性问题，所以要跟他抬杠的话，只需要指出一些反例就可以了。当然对基督徒来说，更关键也是更自然的，就是告诉他将来的世界，根据圣经，是「新天新地」，所以他的归纳法不能用，就可以了。<br />
<br />
基督的品格&nbsp;&nbsp;从这里开始，罗素进入了为什么他不是基督徒的第二大理由的阐述，即基督并不能令他信服。我想因为修辞的原因，罗素先还假惺惺地要说两句基督的好话，以给人公允的感觉。不过，这除了迎合当时听众中反基督教的狂热情绪以外，大约就只能骗骗对基督教毫无了解的中国知识分子了。Jager说他常常把靶子故意“不负责任地夸张（unduly inflated）”，[xii] 我倒更倾向于Brightman的看法，认为罗素因为过早就太轻率地放弃了对宗教问题的探求，所以很多问题其实根本就是不懂。[xiii] 当然，考虑到罗素的极端个性，即对凡与他意见不同的人，他都要极尽抹黑之能事，[xiv] Jager的看法也不无道理。<br />
<br />
比如罗素在这里举了基督的四句话，以证明他本来多么欣赏基督，但基督徒们太不争气。当然，我们承认历史上有很多自称为基督徒的人的行为并不荣耀基督。但是若我们同意信仰的核心不是由信徒的行为来定义的话，那么这自然不成为罗素不愿意成为基督徒的理由。不过罗素显然不是要在这里作这样的思考，因为如果仔细看他所举的例子和他所作的引申，若往好里说的话，我们可以说他真是不懂，因为这涉及到一些释经学原则的问题；往坏里说的话，则他是故意怀着恶意了。罗素举的第一句话是基督在著名的登山宝训中的一句，即所谓“别人打左脸，把右脸也给人打”。基督在这里教导信徒不要以恶抗恶；罗素却解释为要纵容邪恶，并进而指责何以基督徒们都没有纵容。第二句是基督关于不要论断的教导；罗素扭曲为不可伸张正义，于是社会上伸张正义的基督徒在罗素眼里都变成不遵从基督教导的了。第三句是基督关于无伪的爱心的教导；罗素则解释为放弃原则的乡愿，于是基督徒按照正常的经济学原理进行财政管理的辩论也成了与基督的教导相违。最后，罗素把基督关于舍己的教导等同于要人抛弃一切物质的财富，这样当然又有一大片基督徒不合格了。从这几个简单例子可以很清楚地看出，罗素或是出于无知，或是处于故意，把基督的话作了“不负责任的夸张”，因而其攻击除了迎合一些本来就很反对基督教的人以外，就是蒙蔽对基督教不甚了了的人了。他后面的攻击，基本上都是同一手法，我们下面一个一个来看。<br />
<br />
基督教导的缺点&nbsp;&nbsp;虽然说要讲几点，但罗素在这里实际只讲了一点，即他认为基督教导说，k的第二次来临是在门徒们的有生之年；但事实上这并没有实现，所以基督并不聪明。但这明显是他的误解，因为基督并没有这么教导。他举的几个例子，根本就是把几种情形硬按自己的意思拉在一起。比如，他举的“以色列的城邑，你们还没有走遍，人子就到了。”一句，出自马太福音10：23，情景是基督差派他的门徒去宣教。就上下文来看，基督丝毫没有教导关于末世的事教导，而是坚固门徒受逼迫时的信心，即应许会随时拯救他们。“站在这里的，有人在没尝死味以前，必看见人子降临在他的国里。”同样出自马太福音16：28，马可、路加福音也都记载了类似的话，根据下文的记载，很显然基督是指着k登山变像说的。最后，对“不要为明天忧虑”一句，普天下大约只有罗素一个人才能作如此天才的释经了，因为正常的释经学者都知道这是基督为了教导关于天父护理的恩典，并坚固信徒的信心而说的。事实上，基督明白地教导说，他再来的“那日子，那时辰，没有人知道，连天上的使者也不知道，子也不知道，唯独父知道。”（太24：36）他怎么可能又说，那日子就在门徒们的有生之年呢？<br />
<br />
另外，罗素在这里也提到耶稣基督的历史真实性。虽然从圣经研究史来看，的确在历史上曾有一段时期，有一些圣经学者对此提出过怀疑。但按罗素对宗教的认知程度，我很怀疑他在这里讲的是否是这些圣经批判学的内容，还是只是信口开河而已。但无论如何，现在主流圣经学者已经抛弃这些过分极端的看法了。[xv]<br />
<br />
基督的道德问题&nbsp;&nbsp;正如多位作者都指出一样，让罗素来评论别人的道德这件事本身就很滑稽，这不单因为罗素自己的道德生活一塌糊涂，[xvi] 而且就在《为什么我不是基督徒》同一本书所收录的另外一篇文章，“我相信什么”中，罗素就明明白白地宣称，“严格说来，我并不认为有所谓伦理知识这件事。”[xvii] 换句话说，真要按罗素的哲学来跟他较真的话，他这里所说的一切不过是“我不喜欢基督”而已，跟“基督的道德究竟如何”毫无关系。当然，在群情激昂的大众面前，他就很“智慧”地把自己的哲学收起来了。<br />
<br />
看明白了这一点，我们对罗素攻击的所谓基督的道德问题就比较容易看清楚其诡辩的地方了。他所谓“重要性很大”的问题，无非是说基督教导了最后的审判。试想一下，我们若看见一个人要干坏事了，于是告诉他说法网恢恢，并严厉警告他不要玩火，这能算是我们的道德问题，而且是很严重的道德问题吗？另外，「按照法律发出警告」与「滥用法律」、甚至「冒用法律、私设刑堂」，是完全两回事，罗素居然要前者为后者负责，真不知道他的道德观是从哪儿来的，――肯定不是地球！呵呵。<br />
<br />
他所谓“重要性较小”的问题当然也没什么新意。要知道，基督把鬼赶出来以后，鬼总要逗留在某种地方，无论是猪群也好，树林也罢，还是就在大地上游游荡荡。我相信无论那种情况罗素都找得出来说词。比如说，让鬼“走开了事”吧，那么我们的罗素先生一定会说基督做事不彻底，不负责任，因为鬼在外面继续游荡，万一附到了罗素先生身上怎么办？而至于让鬼到猪身上，我实在看不出来罗素吃猪肉比这仁慈了多少。总之一句话，罗素在这里是在用修辞的手法引导听众和读者的情绪，并没有任何有意义的思考。对无花果树也一样。罗素同样用了移情的手法，先把树木拟人化，赋以人的情感，然后再替无花果树叫曲。但问题在于，只有有位格者，比如人，才能谈论诸如公义、是非、曲直这一类的伦理问题。否则的话，罗素每次写文章的时候，他是不是也要考虑他桌子上的纸如何痛，笔如何委曲呢？基督在这里以无花果树为工具来说明以色列人悖逆神的结局，与罗素砍一段木头做桌子有什么差别呢？何以到基督身上就变成了道德问题？<br />
<br />
情感因素&nbsp;&nbsp;罗素对宗教中的情感因素指责很多，大约是因为他自己在这方面很敏感，也很善于调动和利用大众的情感吧。因为他这里前半段的手法，就是利用情绪的感染力来达到说服效果的绝佳范例――他毕竟是诺贝尔文学奖获得者。从逻辑上来说，他讲的这个故事完全以「根本就没有上帝」为事实前提，因此丝毫不具说服力。他的后半段更是打胡乱说。我们不否认，历史上有很多在基督教名义下做的错误事情，但这不等于基督教对人类文明完全没有贡献――要按这种逻辑，没有任何东西对人类文明有过贡献了。事实上，犹太―基督教文化与希腊―罗马文化是西方文明不可缺少的两条腿，乃众所周知的事情。[xviii] 文化、社会、人类学者在这方面的研究可谓汗牛充犊。就以罗素提到的废除奴隶制度为例，在英国，这件事的成就恰恰就是基督徒政治家威伯福斯（William Wilberforce）不懈努力的结果。[xix] 更具讽刺意味的是，恰恰就是罗素随意发放肤浅之辞，也引得其知识分子对罗素崇拜得五体投地的中国，她的近/现代化进程就从基督教那里得到了关键性的帮助。[xx] 这些恐怕都是罗素在受情感因素控制时不能明白的事。<br />
<br />
教会是怎样阻碍进步的&nbsp;&nbsp;用Brightman的话说，罗素在这里的逻辑是“故意地制造借口”，[xxi] 因为个别的例子并不能作为整体教义的考量。否则的话，任何一次医疗事故都可以作为「医学阻碍人类进步」的证据，任何一次科学探索的失败（比如挑战者号的爆炸）都可以作为「科学阻碍人类进步」的证据了。至于历史事实方面，我想上面一段的回答已经够了。<br />
<br />
恐惧是宗教的基础&nbsp;&nbsp;罗素在这里的高论当然经不起任何历史事实和逻辑的推敲。比如，罗素肯定没听说过基督教历史上有一类叫做“殉道士”的人，这些人因为“害怕”，以至于在遭受逼迫的时候，宁愿死也不放弃信仰。而整个基督教历史中都贯穿着这样愿意为信仰而忍受逼迫的人（与历史事实矛盾）。又比如，从逻辑上说，有人因恐惧而选择宗教，并不等于所有选择宗教的人都是因为恐惧（逻辑不等价）；心理因素最多只能解释人们为何会选择信仰――所谓“病急乱投医”，但并不能解释为何人们会具体选择这种信仰（逻辑不充分）；最后，人们因害怕而做一件事，并不等于这件事就一定不好。比如，罗素因为害怕赤脚走路扎到钉子所以穿鞋，这是否意味着罗素穿鞋这件事就大逆不道呢（逻辑不必要）？实际上，所谓「恐惧是宗教产生的原因」不过是在西方思想史上，曾有一群鼓吹所谓“实证主义哲学”的人所倡导的说法，而现在早已被主流学界抛弃了。<br />
<br />
我们要做什么&nbsp;&nbsp;在一大群狂热的听众面前，罗素当然把人类说得充满希望，“人定胜天”。他大约想不到过不到十年，同样的人类在他笔下就已变得毫无价值、丑陋、残忍、好斗，比动物都不如了。[xxii] 当然，无论那种情况，罗素都是作为导师在对人类进行语重心长地劝诫的。[xxiii]<br />
<br />
结语<br />
<br />
考虑到罗素在宗教问题上实际认识的肤浅，这篇演讲其实并非那么难回答。正如Smith和Bahnsen所指出来的那样，罗素自己的哲学并不能给他提供任何对宗教问题进行批判的基础。他后来的逻辑原子主义（Logical Atomism）给出的形而上学其实也相当混乱，[xxiv] 而他1949年出版的《人的知识》（Human Knowledge）更被学术界评为“巫婆的唠叨。”[xxv] 甚至他的辩护者Jager虽想极力证明罗素在宗教问题上的看法不那么肤浅，也不得不痛苦地承认，至少罗素的后半生在这上面乏善可陈。[xxvi] 而罗素早期的所谓“洞见”则更多应归因于他思想中的柏拉图主义因素，但这些因素后来却为他所抛弃。这样一来，他就只能陷入更深的不可知论，遑论任何有意义的价值判断了。<br />
<br />
作为哲学家的罗素如果老老实实地呆在书斋里倒也罢了。然而他却是不甘寂寞的人。在哲学上明显江郎才尽的时候，他转向普罗大众以继续满足他的虚荣心。他甚至自己都承认，写那些东西纯粹是为了赚钱。[xxvii] 但问题在于，这样持之以恒哗众取宠的结果，就是<br />
<br />
象1920到1930年代的爱因斯坦一样，对世界各地的普罗大众来说，罗素成了抽象哲学家的精华、原形，思想机器的体现。什么是哲学呢？那，就是伯兰德罗素谈论的那些事儿。[xxviii]<br />
<br />
更不幸的是，罗素1920年代对中国的访问深深影响了这个国家一代甚至几代的知识分子。在北京大学逗留一年，留下了一系列与其说是基于他对中国的认识，不如说是基于他对西方文明的牢骚而“托物言志”的高谈阔论之后，[xxix] 这位“人类的导师”挥挥手带走的，却不单是一片云彩，而是这文明古国在她痛苦的现代化寻求中一片迷惘的眼睛。中国知识分子何时才能从这些二流牙秽中清醒过来，真正地“睁眼看世界”呢？<br />
