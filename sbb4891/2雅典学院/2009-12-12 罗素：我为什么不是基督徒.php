&gt;&gt;&gt;&gt;&gt;&gt;<br />
这部分内容，实际上就是硬性规定了上帝是不准许质疑的，因为上帝不等同于世界，所以人类对世界的一切认识都不能用来质疑上帝。而人类除了对世界的认识，又不可能有别的认识。即便根据基督教，我这句话也基本上是事实，只不过基督教中上帝还可能（至少是间接地）给人类以启示。换言之，就是除非上帝启示你，让你质疑上帝，否则上帝就是不准质疑的。<br />
基督徒反过来掉过去所说的话，其最重要的原理就在于此：设置一个不准质疑的上帝。因为上帝的定义不许质疑，所以你质疑上帝，就等于是打破了讨论的规则。（比方说，人家已经定义某动物为马，你非要管它叫驴，那总是你的不对。）<br />
基督徒狡猾不狡猾不论，这是不是说罗素的理解就是错的呢？<br />
不是。<br />
