													<h2>你读懂了韩寒 却没读懂许知远</h2>
						
						
																											<div id="postmessage_612835" class="t_msgfont">许知远写了一篇《庸众的胜利》，韩寒迷们和韩寒爱好者就开始为韩寒辩护，其实，许文章对准的是民众、民族，而不是韩寒，他并不要求韩寒去做LXB，也没有贬低韩寒，他对韩寒的分析也基本客观的。他批评的只是对民众的心态。韩迷和韩寒爱好者，未看清对象就开始为韩寒辩护。或许潜意识里面认为，偶像兄已经都这样了，谁还能要求他怎样？是的，问题是，许知远没要求韩寒怎样。他只是在叹息。他朝韩寒身后的庸众扔了一块泥巴，却被韩寒爱好者们吐了一脸口水，（不排除两者之间有交叉）。<br />
<br />
 <br />
<br />
许知远批评躲在韩寒身后的庸众们，鼓掌叫好的人，而自己可能会永远不下场子，看韩文章出了一口恶气后，照样在生活中做自己的顺民，甚至做帮凶。杀君马者道旁儿，韩寒或许在他们的赞扬声中走向“深渊”，庸众们或许会有些悲伤，擦把眼泪，继续寻找下一个偶像兄。韩寒不过是另外一种消费品。许叹息的是，这沉默的大多数，才是社会变革的关键。他们本该自己担当，而不仅仅只是欣赏韩寒，而已。<br />
<br />
 <br />
<br />
许没有谈到的是，韩寒所作的意义，部分是启蒙性的，部分是代言性的。这种有技巧的启蒙胜过很多张报纸。这种代言汇聚了一定的民间意见，对执政者也是一个威慑。作为一个文学青年和车手，你不能要求他做更多，所以，许知远也没有要求他做更多。我想任何人都不能要求他做更多，除非他自己愿意。<br />
<br />
 <br />
<br />
韩寒是一个启蒙者，是一个天才的杂文家，他的能量作用摆在眼前，他的“局限”许知远已经指出。任何人都有局限，尤其在这个时代。粉丝们的心情能够理解。当下，韩寒的言就是行，已经够勇敢。L在多年后也是以言代行，因为某个特定的时候，言比行更厉害，尤其在传播面上。现在的AI大胡子，算是一整个实干者了，不过，他在推特上简直是话痨之王。龙生九子，子子不同。整齐划一会是噩梦。<br />
<br />
 <br />
<br />
回过头来，为什么庸众会胆子小，是因为威权者胆子大。威权者胆子为什么大，因为庸众胆子小？鸡、蛋理论。作为庸众，有家有小，自然可以理解压力，庸众们围观韩寒的嘲讽能力和勇气，为之叫好，也是一种出气和解脱。庸众要学韩寒，既需要勇气又需要本钱，更无力去学习更高端、更危险的LXB，这点，想必绝大多数自命为精英的人也不会学L。这就是张培鸿评论一针见血地指出，为什么你许知远不去学刘。<br />
<br />
 <br />
<br />
进一步的是，即使想学L，也未必能学得会，学得成。如姚明去练长跑，甚至适得其反。许在其精英圈中启发，深刻度当在韩寒至上，韩寒在大众权中启蒙代言，普及度远在许知远之上，AI大胡子在搞路演发录像，石破天惊。L在修自己的龙象功，渐到九重。尺有所长，寸有所短。作为决定性力量的庸众，粉韩之余，该量力而行，努力自爱，学习公民韩寒，精英许知远，艺术家大胡子，龙象功L等的精神、原则，然而，等这一切一旦聚汇成流，问渠那得清如许，为有源头活水来。社会或许能走出烂泥塘，当然，对手也未必在吃干饭，朝烂泥塘里注污水，扔石头，筑堤坝，也很忙乎。<br />
<br />
 <br />
<br />
很多人读懂了韩寒，因为文字犀利。很多人没读懂许知远，或许是文章晦涩，或许是感情冲动。如培鸿，虽说他不是韩寒的粉丝，看了他的文章，我觉得他作为一个韩寒爱好者，也还是动了感情。有感情，是好事。只是，别用错了地方。<br />
<br />
 <br />
<br />
 <br />
<br />
 <br />
<br />
张培鸿的评论：<a href="http://www.zhangpeihong.com/" target="_blank">http://www.zhangpeihong.com/</a><br />
<br />
 <br />
<br />
 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;再看许知远的评论。坦率地说，这是一篇相当深刻地剖析“韩寒现象”的文章，条理清晰、一针见血。总结起来有两点：一是韩寒从不涉及真正敏感的话题，显得相当聪明；二是韩寒缺乏行动力。特别对于韩寒在《时代周刊》评选中得票超过刘先生，许知远尤其不以为然。<br />
<br />
 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;这篇文章独到的地方还不在这里，许文将批评的焦点从韩寒身上移开，放到了普罗大众身上。认为韩寒的流行，意味着大众都只是些庸庸碌碌的“墙头草”。只会选一些安全的角落，放声地嘲讽现实。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 对此我有完全不同的看法。诚然，韩寒有自我保护的意识和底线，问题是这算好事还是坏事？我们首先要求一个人长得帅，他做到了；然后我们要求他车开得好，他又做到了；我们进而要求人家文字漂亮，他也做到了。这时候没有话说了，却指责他没有去抱炸药包，非要拿他去跟没有他帅、不会开车、文章没有他的好看但比他猛的人去比，会不会过于苛刻了？<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;事实上，即便是许知远笔下所谓的“庸众”，我现在也认为，只要他们看到韩寒的文字，脸上还会漾开会心的微笑，我们这个社会就大有希望。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;写下这堆话，不是为了顶韩寒，因为韩寒不需要顶。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;我只是突然想到钱锺书的那个比喻，如果你喜欢吃鸡蛋，为何要认识下蛋的母鸡？或者说，你如果要苛求母鸡的蛋，何妨自己也下一只？</div>

							
							
							
							
