													<h2>假如甘地活在今日之中国……</h2>
						
						
																											<div id="postmessage_172813" class="t_msgfont">羽戈&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;　　<br />
　　<br />
　　……恶吮吸的是坚固。它永远借助大数目，借助可靠的花岗岩，借助意识形态的纯正，借助训练有素的军队和均匀的裹尸衣。它借助这类东西的癖好应该说与它内在的不安全感有关，但是，相对于恶的胜利来说，明白这点同样难以获得多少安慰。<br />
　　――约瑟夫・布罗茨基<br />
　　<br />
　　<br />
　　一<br />
　　<br />
　　据鲁迅先生之子周海婴所述：1957年，毛泽东到上海小住，依惯例找几位老同乡聊天，周谷城、罗稷南等人参加了座谈。时值反右，谈话内容涉及文化界人士在运动当中的表现。罗稷南抽了个空隙，向毛大胆提问：要是今天鲁迅还活着，他可能会怎样？毛的态度十分认真，沉思了片刻，回答说：以我的估计，要么是关在牢里还要写，要么他识大体不作声。一个近乎悬念的询问，竟得到如此严峻的答案。罗稷南顿时惊出一身冷汗，不敢再作声。<br />
　　这段逸事很出名，后人研究反右史或知识分子史，总喜欢以此为由头来隔山打牛。也许，正因为历史残酷到不容假设，人们反倒更愿意沉浸于假设的迷情。但这样的设问，并不只具有意淫的意义，还生发了批判现实的锐利光芒，乃至拓宽了历史学既定的疆域。1936年去世的鲁迅，活到1957年会如何，历史没有回音，亡魂沉寂无声，我们却看到鲁迅的精神传人从黑暗之中挺身而出，用自由和生命铸造着惟一的答案。<br />
　　与其说鲁迅活在1957年会如何，不如说鲁迅精神活在1957年会如何。<br />
　　无端想起这些，是因为如今距离M.K.甘地先生辞世，恰好六十年；是因为在汶川大地震期间，我读到穆旦作于1948年2月4日的《甘地之死》，联系起近若咫尺的灾难――而甘地的逝世何尝不是一场比地震还要可怕的灾难――这首诗的第一节是如此痛彻心扉：<br />
　　<br />
　　不用卫队，特务，或者黑色<br />
　　的枪口，保卫你和人共有的光荣，<br />
　　人民中的父亲，不用厚的墙壁，<br />
　　把你的心隔绝像一座皇宫，<br />
　　<br />
　　不用另一种想法，而只信仰<br />
　　力和力的猜疑所放逐的和平，<br />
　　不容忍借口或等待，拥抱它，<br />
　　一如混乱的今日拥抱混乱的英雄，<br />
　　<br />
　　于是被一颗子弹遗弃了，被<br />
　　这充满火药的时代和我们的聪明，<br />
　　甘地，累赘的善良，被挤出今日的大门，<br />
　　<br />
　　一切向你挑战的从此可以歇手，<br />
　　从此你是无害的名字，全世界都纪念<br />
　　用流畅的演说，和遗忘你的行动。<br />
　　<br />
　　――甘地真的成为了“无害的名字”？我们真的“遗忘你的行动”？对勘光怪陆离、花果飘零的新世界，不由使我设问：假如甘地活在今日之中国，他会怎么样呢？甘地精神是被发扬广大，还是如许多伟大的真理或谬论一样，在华夏热土零落成泥，湮没成尘？<br />
　　<br />
　　二<br />
　　<br />
　　甘地精神已经与“非暴力不合作”划上了等号。<br />
　　“非暴力”是外形，“不合作”是实质；“非暴力”追求程序正义，“不合作”追求实质正义。可以对比它的两个邻居，“暴力”而“不合作”即革命，“非暴力”而“合作”即顺服，倒未必全是奴隶式的，改良者亦会选择这种方式与当权者与虎谋皮，喜剧政治学的坚守者亦会以过分的、变态的“合作”行动，来嘲讽、消解恶的价值，使恶荒唐而不名一文。“非暴力不合作”既非革命，更非改良――它与后者的歧异在于对统治阶级的信赖程度，一者相信当权者会在自己的教化、引诱之下有可能走向善好的制度，一者则认为从内部无法改造当权者的气质，所以必须建构“不合作”的压力，从外部逼迫其进行政治整形手术。那些判定甘地为改良主义者的人们，多半没有注意到这一隐晦的历史细节：甘地对英国殖民者的信任度并不高，而依他的出身和经历，完全可以选择与当局“合作”，就像与他同时代的中国的戊戌巨子们。<br />
　　从字面上看，“非暴力不合作”不难做到，似乎人人皆能，但甘地何以因此被万众尊称为“圣雄”呢？则涉及“非暴力不合作”的本质：一种道德的、乃至具备宗教纬度的社会整合运动。它所追求的不是以恶对抗恶，亦非以善对抗恶――在甘地看来，过激的“对抗”本身就不是善的――而是以善消融、驯化恶。用中国的一句老话说，叫“以德服人”。德至上者，是为圣人。甘地的形象，颇接近《论语》里的“圣人”。<br />
　　“非暴力不合作”的道德属性，不在于甘地的论说：“有一件事在我心中是根深蒂固的，就是深信道德为一切事物的基础……”而在于这种思想的社会运动形态。不妨看一下甘地的信徒如何贯彻圣雄的思想。1930年，为抗击英国殖民者对食盐之分配权和赋税权的强硬垄断，甘地发动了“向食盐进军”，与信众一同到丹地海岸自制食盐，最终导致包括甘地在内的数千人入狱。在狱中，甘地写信说：“一位主张非暴力的人，手握食盐，象征印度的荣誉。紧握盐的手可被砍断，但他决不交出食盐。”更可观的是他的信奉者，被后世誉为“自由的圣女”的女诗人萨罗吉尼・奈杜，她率领2500名志愿者向苏拉特进军，准备占领那里的巨型盐场。美联社记者韦布・米勒记录了一幕惊心动魄的历史剧情：“甘地的信奉者们默默地前进，在离围栏大约100码的地方停了下来。经过挑选的一队志愿者从人群中走出来，他们越过壕沟，向铁丝网靠近……突然，一声令下，一大群印度警察扑向迎面而来的示威群众，他们手中的包铁长棒雨点般地落在志愿者的头上。没有一名示威者举起那怕一只胳膊抵挡一下落在头上的棍棒。他们像九柱戏里的木柱一样栽倒在地……但示威者只管前进，直到被打倒为止。”<br />
　　面对棍棒，没有反抗，哪怕被打倒在地，哪怕伤残，死亡。但甘地及其信徒深信，在空中肆意挥舞的恶的棍棒，终有一刻，会在他们强大而坚韧的善与爱的磁场面前，羞愧，自卑，缓缓落下，恢复和平的情状，像泛黄的落叶回归秋天的大地。而他们所需做的，即纵然手断、血流、身殒，仍决不放弃所追逐的正当的权利和事物。甘地与他的“非暴力不合作”运动是世界的盐。<br />
　　对此，再也找不到比马丁・路德・金――他无疑是甘地之后又一位“非暴力不合作”精神的伟大守望者――更精彩的论述：“我们将以自己忍受苦难的能力，来较量你们制造苦难的能力。我们将用我们灵魂的力量，来抵御你们物质的暴力。我们不会对你们诉诸仇恨，但是我们也不会屈服于你们不公正的法律。你们可以继续干你们想对我们干的暴行，然而我们仍然爱你们。你们在我们的家里放置炸弹，恐吓我们的孩子，你们让戴着KKK尖顶帽的暴徒进入我们的社区，你们在一些路边殴打我们，把我们打得半死，奄奄一息。可是，我们仍然爱你们。不久以后，我们忍受苦难的能力就会耗尽你们的仇恨。在我们获取自由的时候，我们将唤醒你们的良知，把你们赢过来。”<br />
　　“非暴力不合作”的另一大属性，则是对法治原则的恪守。哪怕他们所生存的国家的法律呈现了一种明目张胆的恶，仍有必要承认“恶法亦法”。可以用“不合作”的形式反抗法律里的恶，却不能用暴力的利器将法律机器全盘砸碎。这是对程序正义最起码的尊重――而“非暴力”最悲观的维系，就是那么一点稀薄的程序正义。说到底，“非暴力不合作”运动必须在法律范畴之内进行运作，稍稍越位，便违背、损害了题中应有之义。<br />
　　很多人会质疑，若实体正义毫无指望，要程序正义有什么用呢？这等于是说，我们连肚子都填不饱，为什么还要整天穿上那件梳洗干净的外套呢？莫非是一种对苦难虚伪的掩蔽？甘地回答，不是这样。他毕生主张印度独立，然而他从未否认过对英国宪法的忠诚。后来他承认，他对于真理的爱，即根源于这种忠诚。“我一生之中，从来不曾滥用这种忠诚，从来没有想借忠诚为手段，来达到自私的目的。就我来说，忠诚毋宁是一种义务，我表示忠诚并不指望报偿。”<br />
　　视其为义务，是不是太过谦卑？但这正是“非暴力不合作”的精义所在，正是它区别于其它运动的要点所在。缺乏了这一丝忠诚，现实的正义将与未来的不义泥沙俱下。缺乏了这一丝忠诚，甘地距离希特勒和毛泽东便近了一步。<br />
　　“非暴力不合作”的第三个要件，是它必须建立在积极的、乐观主义的、对希望和真理持有虔信的土壤之上。可以想象，如果不相信善能消融恶，战胜恶，有多少人愿意接受棍棒的洗礼，一次次被打倒在地仍匍匐前行，不达目的誓不罢休？――除非是受虐狂。一言以蔽之，“非暴力不合作”不是消极的，而是积极的――即便是消极抵抗，如绝食、罢工、罢市，亦裹挟了积极的目的，1919年4月6日，在甘地的领导之下，整个印度举行哀悼日，印度人关闭了商店停止营业，走出了学校进行罢课，或者到寺庙祈祷，或者干脆闭户不出，以示声援。“让整个印度沉寂无声吧！让印度的压迫者们聆听这沉默的启示吧！”甘地喃喃地祈求神灵。同样， “非暴力不合作”不是逆来顺受的，而是锐意进取的；不是基于悲观主义的，而是基于乐观主义的；不是无所期盼的，而坚信至高无上的神灵会惩罚所有的不义，拯救所有的义人，坚信希望和真理之光终将普照在他们虔敬的头顶。譬如甘地的自传书名，就叫“我体验真理的故事”。他视真理为一切道德的本质，为他唯一的目标。“真理的范围逐日扩大，而我对真理所下的定义也随之而广阔。”<br />
　　1925年，甘地写自传的时候，已经把“非暴力不合作”放到了一生所体验的真理之第一位。<br />
　　<br />
　　三<br />
　　<br />
　　然而，“非暴力不合作”并不是可以治疗百病的灵药宝丹，不是放之四海而皆准的普世真理。它有特定的受众，而在我们生活的世界，它的受众正日渐稀少，滥竽充数的只是盲目而虚无的苦行者。它还有其局限。上面所述的三个要点，每一点都标明了限度之所在：宣讲道德，对那些不讲求道德的人，无疑是对牛弹琴；宣讲法治和程序正义，碰到不守法的专制者，只可能强化他们的罪恶心理和迫害欲望；而对于这个时代多如过江之鲫的虚无主义者而言，积极的真理有什么意思？还重不过撒哈拉大沙漠的一粒沙尘。<br />
　　狄马兄撰有《甘地的限度》，所论十分精辟。其中讲到这么两个故事：<br />
　　二战爆发后的1938年11月，甘地向犹太人推销“非暴力不合作”的思想，来对抗希特勒和纳粹主义，著名的犹太哲学家、神学家马丁・布伯觉得很滑稽，就写信反问：“圣雄，你知不知道，什么是集中营，那里发生着什么事？集中营里有哪些折磨人的刑罚？有哪些缓慢和快速杀人的方法？”最后，他痛切指出：“对于那些不明事理的人，可以采取行之有效的非暴力态度，因为使用这种方式有可能使他们逐渐变得明智起来。可是要对付一个万恶的魔鬼就不能这样。在某种情况下，精神力量是无法转化成真理力量的。‘殉道’一词意味着见证，可是如果没有见证人在场又该如何呢？”<br />
　　1939年9月，甘地给希特勒写了一封信：“我的朋友们敦促我，以人道的名义给您写一封信。我至今无法满足他们的愿望，因为感觉告诉我，给您写信是对您的冒犯。然而现在我发现，不能再顾及这种感觉了。事关重大，我必须向您呼吁。我完全清楚，您是全世界惟一有能力阻止一场毁灭人性的战争的人。为了一个看起来似乎非常有价值的目标，难道必须真的付出这种代价吗？难道您不愿倾听一个经过深思熟虑曾经成功地避免了战争的人的和平呼声吗？……”<br />
　　我再补充一个：<br />
　　中国抗日战争前夕，甘地在接见访问者的时候推销他的观念：“不管怎么说，中国不是在实行非暴力。它能英勇对日抗战，说明中国从来没有非暴力意愿。说它只是自卫，从非暴力原则来说，这不是理由。”“从非暴力主义者的立场来看，我必须说，以一个拥有四亿人口的中国，来对付一个开化了的日本，还是不得不以日本人的同样手段来抵抗日本侵略，我以为这是不适当的。假如中国人有我这样的非暴力信念，就不需要和日本人一样的最新毁灭手段。中国人可以告诉日本人，‘带着你们的毁灭手段来吧，我们以两亿人给你，可是剩下的两亿人我们是不会屈服的。’假如中国人真的这样做了，日本人就会变成中国人的奴隶。”<br />
　　――战争被阻止了吗？希特勒、日本侵略者吸纳了甘地的呼声吗？我们都品尝到苦涩的业果。甘地与希特勒，圣雄与恶魔，这两个人根本就不具备对话的根基；而“非暴力不合作”与法西斯主义的沟通更近乎鸡蛋撞石头，不是两败俱伤的与汝偕亡，而是一厢情愿的粉身碎骨。甘地与“非暴力不合作”的限度在此得以清晰展现：在湮灭了人性的事物和制度的铜墙铁壁面前，它们注定败北。<br />
　　同理，“非暴力不合作”对付暴政和那些相信“枪杆子里出政权”的人们，并没有多大的胜算。窃以为暴力最可怕的地方不在于以暴力杀人，而在于将世间一切都纳入暴力逻辑的沉潜往复，包括人性和道德，最后致使用暴力孕育暴力。甘地召唤人性和道德的复苏，进而推动制度走向善好，暴力论者对此会怎么看呢？也许选择了与甘地合作，如果那样正有利于它们的统治的话；但更多的时候，还是悍然举起了屠刀，所以我们看到甘地曾多次被英国殖民者抓进监狱，甚至被印度国内的反对派暴徒侮辱、殴打。它们的主张很明确，无论什么问题，最终都以暴力解决。<br />
　　好在英国殖民者长有两副面孔，一面是暴力侵略，血腥镇压，一面是民主、法治和宪政的国家景象――诚如约瑟夫・布罗茨基在1984年的一次演讲当中所言：“实践这个概念（即“非暴力不合作”）需要有充分的民主，而这正是地球百分之八十六的地区所欠奉的。”――英国则属于那百分之十四。因此他们一面对甘地和“非暴力不合作”的信徒进行打压与放逐，另一面，却受迫于国内被激起的怜悯心和羞耻心，受迫于汹涌成潮的公义，受迫于普通法的法意，只得偕甘地走上谈判桌。典型如慷慨激昂的温斯顿・丘吉尔，他反对英国放弃印度，曾激烈抨击甘地为“半裸体的游方僧”：“昔日伦敦律师事务所的律师，今天衣不遮体的滋事生非的苦行僧，竟然踏上副王宫殿的台阶，平起平坐地与英王兼印度皇帝的代表谈判，这是多么令人作呕，令人感到耻辱的场面。”但他却无法阻挡1931年“甘地-欧文协议”之达成，那次谈判长达三个星期之久，共举行了八次会议。协议条文酷似由两个主权国家所签署，确认了甘地是斗争的胜利者，答应释放数千名印度人，他们在“向食盐进军”的抗议运动之中与其领袖一起被捕入狱。他更无法阻挡，八个月后，即1931年10月，甘地身披一块粗糙腰布，“前不遮胸，后不遮背”，脚穿拖鞋，步入白金汉宫与英王兼印度皇帝一起呷茶交谈。当被问到如此着装是否适宜之时，甘地诡谲地回答：“国王陛下有足够的衣服供我们俩人受用。”会谈尽管以失败告终，甘地仍乐观预言，真正的胜利“需在谈判会场之外取得……今天播下种子，来日定会缓和英国人的立场”。而丘吉尔只能拒绝与甘地会晤，以表示他的不满。<br />
　　“非暴力不合作”的幸与不幸皆在于此。它的诞生就是一种不幸。生于20世纪上半叶的印度，还是英国，如果可供抉择，相信几乎所有人都情愿站到后者一边。可甘地生在了前者，他必须擎起“非暴力不合作”的明艳旗帜，这是命运之神压到他的背上的自由的重负。幸运的是，他的对手是宪政主义的英国，是丘吉尔这种自由主义政治家。尽管丘吉尔声嘶力竭地叫嚷： “丧失印度，对我们是决定性的致命一击。它使我们逐渐变成一个微不足道、黯然失色的国家。”但他反感甘地，只能选择礼节性的避而不见，而没有一声令下，将甘地再度抓捕入狱，或者对甘地进行身体和政治迫害，乃至直接暗杀。也许，他没有这个权力；更可能，他的骨子里根本就不会使用这种思维。<br />
　　试想，若把丘吉尔换成希特勒，结局当如何？<br />
　　还需要指出一点，一个弥漫着悲观主义与虚无主义空气的社会，亦可能构成“非暴力不合作”向上突破的瓶颈。甘地与他的跟随者多半受基督教和佛教精神的超验滋养，头上的灿烂星空成全了心里的道德律令，对神灵的虔信暗示了救赎在明天的可能。但在一个无根基、无信仰的时代，统治人们头脑的只有金钱、肉欲等工具理性，这般严酷的语境是否适合“非暴力不合作”精神的生长？我给不出肯定的答案。因为就我所见，在此情境之下，那些道德上的苦行者，或者被指认为骗子，或者苦练精神禁欲术而成为隐士，或者即因为道德高标而将自己送上了受难的祭台。可以断言，它产生不了甘地，而代之以鲁迅。阳光与阴冷两种人格，映照此世黑暗如铁。<br />
　　最后，“非暴力不合作”最大的难题，在于如何避免陷入内心的迷狂。我想举一个例子。二战时期，当德国人的猛烈炮火在伦敦上空隆隆作响的时候，甘地向英国人民提出了比给希特勒写信请求停战更加耸人听闻的建议：“你们可以邀请希特勒和墨索里尼随意征服你们的国家，征服你们称作为属地的国家。你们也可以让德国人踏上你们的美丽宝岛，占领你们的无数富丽堂皇的名胜古迹。让他们任意占领吧，但你们决不可丧失心志。”――“非暴力”即不抵抗，“不合作”即借助精神力量在内心抗争野蛮的侵略者。看看甘地为推销品牌思想而打的如意算盘，英国人会怎么想呢？丘吉尔会怎么想呢？视之为怪僻老儒的无稽之谈？甘地差点因此被拘捕监禁。<br />
　　<br />
　　四<br />
　　<br />
　　好了，让我们回到正题，来严肃对待这一番思想神游：假如甘地活在今日之中国……<br />
　　对于“非暴力不合作”的生长而言，今日中国的土壤固然不够肥沃，却也难言贫瘠，尤其是比照甘地去世之时的六十年前。他若是生活在那个时代的凄风苦雨，肯定没什么好果子吃，思想找不到信众，行动受制于战争、腐败和二元论的意识形态。那时最与他接近的中国人形象是胡适之先生。胡适后来说“容忍比自由更重要”，可以视为甘地精神的华夏接续。甘地自传的结语即断言“非暴力是最大限度的谦让”。可1948年的胡适遭遇如何呢？当时，国民党拿他作棋子，更刻薄的说法是夜壶，内急的时候就提出来，不用的时候就扔到床下，胡适自诩为“过河卒子”，却落得周弃子先生“途穷未必官能弃，棋败何曾卒向前”的嘲讽。共产党则怒视其为仇谶，乃至此后三十年，胡适都被打击为落水狗，他的思想言论不过是正剧以外的荒腔走板。至于民众，除了极少数知识分子，有多少人晓得胡适呢？像甘地那样在印度遭受草根阶层王者一般的拥戴和欢呼，胡适连做梦都无法企及。<br />
　　好在至今日，胡适遗留的精神遗产已经慢慢浮出水面。“非暴力不合作” 肇兴于托尔斯泰和甘地，经由马丁・路德・金而出现一大光明的转折，其中国市场颇具规模，而且有日渐壮大之势（如前两年的贺卫方罢招，陈丹青辞职，皆可视为对教育制度政治化的“非暴力不合作”，这样的例证不胜枚举）。毕竟，民主与法治观念如雨后春笋盛长于人心，布罗茨基那个“百分之八十六”的统计恐怕需要修改。信息流通与公开的程序远胜于六十年前的殖民地印度，甘地不必再一个村庄一个村庄宣讲传道，他不但能够在报刊撰文，且有互联网可以利用，这无疑有助于思想的速效传诵。最关键的是，甘地再也不用担心与希特勒这样的冷血恶魔打交道，连萨达姆都懂得包装自己，与来访伊拉克的国际诗人面谈甚欢，甘地若给他写信，纵使得不到对停战呼吁的响应，却极有可能收到一封热情洋溢而立场坚定的复函。也许，收信人该换成小布什总统或Wen Jiabao总理。因为这个时代的专制暴政已经改头换面。<br />
　　假如甘地活在今日之中国，他在工作上怕是难有大成。忘了说，甘地是律师出身，而且他自认为是一个好律师。写自传的时候，他总结道：“律师的真正职责是使有嫌隙的双方言归于好。这个教训是这样深深地铭刻在我的心里，以至在我执行律师业务的二十年间，大部分时间都花在促使成千的案件私下妥协。”他认为这样做毫无损失，不论收入，抑或灵魂。他还回忆：“当我作学生的时候，就听说律师这个行业是撒谎的行业。然而这种说法对我没有什么影响，因为我并不想以撒谎致富盗名。”――以此两点来观照时下乌烟瘴气的中国律师界和司法界，我们只能建议，请甘地先生换一个工作。他如果执意于那些原则，估计连律师执照都申请不到。<br />
　　假如甘地活在今日之中国，他更难以成为一个优秀的政治家――何况他本来就不是。他的行动总是受思想观念固执的宰制，这种人往往更适合做教师。然而甘地太不重视形象，中国有哪所大学愿意对一个衣衫褴褛的苦行教授兼容并包呢？甘地还可以尝试做传媒的评论家或自由撰稿人，但以他的笔法，写不了两天，就要触到政治防火墙而被有司封杀，或者因退稿信太多而心灰意懒宣告退出江湖――说来奇怪，中国现在的言论尺度好象还比不上六十年前宽松平和，自由撰稿人体味的多是“不自由”的枯涩。当然，甘地还有一条路，即像这个时代的大多数“法政人”一样从事基层的维权工作，以他的专业知识、经验、坚忍和信心，这大概是最适宜他的活计。但如“向食盐进军”这样的大规模社会抗议运动，甘地最好不要涉足，没准就因涉嫌颠覆国家安全罪而被投入监狱。<br />
　　假如甘地活在今日之中国，他是否愿意对“非暴力不合作”有所修正呢？他必须直面这块曾经暴虐到连鬼神都不敢驻足的国土，无宗教传统，无超验意识，无信仰；他更应该正视那些精神长满荒草的八零后与九零后一代，他们对此世的疏离感与冷漠感和法西斯主义一样令人惊惧，甘地该怎样进行教诲呢？他还需看到，这个时代和国度流行的不是正典，不是正谕话语，不是正襟危坐的布道，而是一种喜剧政治学的诞生：不是以恶抗恶，乃至不是以善抗恶，而是以无价值（无厘头）来冲击恶，使恶荒诞化，这就是为什么周星驰的喜剧电影比自由主义的煌煌启蒙著作更容易消解威权政治的符号体系。“非暴力不合作”被布罗茨基拓展为“非暴力”而 “过量的合作”――“通过你大幅度的顺从来压垮恶的要求，可使恶变得荒唐，从而把那种伤害变得毫无价值。”布罗茨基重申，这种胜利不是道德上的，而是生存上的。但毋庸置疑，这更合中国的虚无主义一代的胃口。与此对照，甘地苦行僧般的身影过于严肃，他们至多会敬而远之。<br />
　　假如甘地活在今日之中国，他的前景并不容乐观，如果他不愿顺应时代潮流而有所改进――但若他自磨棱角，扭曲脊背，他便不再是圣雄甘地――我的眼前隐约浮现了这样暗淡的一幕：<br />
　　“甘地远远望着孩子们的身影，一股强烈的痛苦在心中升起。他尽力克制自己的感情，继续朝下一个村庄走去。入夜，天气寒冷潮湿，蜿蜒曲折的小路布满露水，道路越来越泥泞难行……”<br />
　　<br />
　　五<br />
　　<br />
　　1948年1月30日，星期五，甘地遇刺身亡。凶手纳图拉姆・戈德森曾经是甘地的信徒，早年投身于“非暴力不合作”运动，并因此入狱。他后来转变为一名狂热的印度教徒，不但鼓吹暴力和种族至上理念，还积极身体力行。当从“批判的武器”进化至“武器的批判”，甘地有难了。<br />
　　二十年后，1968年4月4日，马丁・路德・金遇刺身亡。“非暴力不合作”思想谱系上的又一颗明星黯然陨落。虽然查到凶手是詹姆斯・厄尔・雷，但他背后是谁，杀死金的密谋真相是什么，至今仍无确证。<br />
　　甘地在世的最后一天，恰好是耶稣受难日。《圣经》说：“收刀入鞘吧！凡动刀的，必死于刀下。”可是这些不遗余力反对“动刀”的人们，为什么一再死于刀下？<br />
　　<br />
