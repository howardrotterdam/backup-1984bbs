													<h2>铅笔经济研究社：工人也是资本家</h2>
						
						
																											<div id="postmessage_654698" class="t_msgfont">铅笔经济研究社 江小鱼 <br />
富士康是一个引子，如今这一波批判“血汗工厂”的浪潮来得比以往更加凶猛。清一色的论调，批私斗资，恍若几十年前的场景。 <br />
即便是相当温和的一些言论，对于企业和工人，亦是先入为主下了强势对弱势的定义，耶鲁大学陈志武教授便有类似的看法，认为企业通过合并规模可以很大，个体的工人是弱势，故此有权组建工会、通过罢工等手段来对抗资本家。他说“公司并购越来越广泛，行业集中度总体上升很多，使各行业公司的控制权增加。如果在这一趋势的同时不允许员工组织工会并有权罢工，员工方就相对越来越劣势。” <br />
陈教授举了个有趣的例子，是耶鲁食堂的工人罢工，导致他们学校师生要光顾校外流动小贩。受到连累“无啖好食”的陈教授认为这是正常的现象，是一种市场博弈。我佩服陈先生的大度，但我反对他的观点。 <br />
食堂工人竟然可以违反合约罢工几个月，而大学方面束手无策，根本原因在于工会一早已经通过政治压力取得“罢工权”的合法性。罢工可以，炒人违法。其实，如果食堂工人所在工会能力够强大，是可以去驱逐流动摊贩的。不要以为这是笑话，实际上，历史上工会的茁壮成长，伴随着的正是恐吓、驱逐、殴打等脏脏手段，当然还有加上黑社会“洗白”过程。我私下揣测：如果流动摊贩被驱赶了，陈志武教授只好自己回家做饭了，说不定还会从中发现自己做饭即可以锻炼身体也更合口味的好处来了。 <br />
里根总统做过演员工会主席，所以深知工会本质是什么东西，他下令开除拒绝复工的万余名航空业罢工者；撒切尔夫人斩钉截铁回击煤矿工会，“有人想用暴民原则代表法律原则，但这决不能成功。”两位伟大政治家的这些决不妥协的举措，奠定了健康市场的基础，为数十年的繁荣种下了种子。 <br />
俱往矣，福利国家的祸害，泛滥成灾（如今的希腊甚至整个欧洲的危机，即是一例）；工会组织肆无忌惮，祸不单行。往事只能回味！ <br />
回到富士康的例子来，一边是财富以百亿计算的郭台铭，一边是数十万平均年收入只有2万的工人，按照上面的逻辑，这肯定是强势对弱势的博弈了，几十万工人，就这样被剥削、被奴役。如果没有强大的工会，工人们永远改不了自己的命运。 <br />
是这样的吗？ <br />
一般民众情绪之下说出这样的话尚可原谅，当然，有一些媒体人员，因为他们的工作就是要“为大多数人说话”，不奢求他们能有符合逻辑的言论。对于受过现代经济学教育的人，还这样认为就令人百思不得其解了。 <br />
让我们来看看这样一个镜头： <br />
大江东去，千帆竞渡，而一些装载货物的船，要一群纤夫在河边拉。有监工在一旁看谁有偷懒嫌疑，拿鞭子抽打。 <br />
滚滚河水，满身大汗的赤裸上身的纤夫，扬起鞭子的监工。这种对比，会让人自然生出对拿鞭子的人的仇恨和对纤夫的同情。对于这个场景，相信大多数经济学人都知道出自张五常教授笔下。孩童时候的经历，让他问了一个重要的问题：到底是拿鞭子的人雇佣纤夫们呢，还是纤夫雇佣了拿鞭子的人？ <br />
带着阶级斗争的眼光去看，是有人奴役欺凌纤夫，要对他进行革命斗争么？纤夫们会和你搏命。因为那个扬起鞭子的人很可能就是纤夫们为了防止有人偷懒而聘请的。船家帮人拉货，然后跟一班纤夫谈好，要在一定时间内把货物拉到目的地。拉纤时候偷懒很容易，可以装模作样，暗中卸膊。这个团队没有效率，大家收入都会收到影响。所以需要一个监工的职位。 <br />
同理，如今几十万的工人，他们有劳动力，愿意付出时间和体力，在动荡年代，他们可能会组织起来，去攻城略地，去占山为王。如今，他们愿意花钱请一个人来，投入资源，组织他们进行生产，而不是打斗、内耗、战争。他们选中了郭台铭，郭台铭得到的报酬，是支付了管理费用、扣除了工人工资、税收之后的收入，很不幸，这种报酬，工人没担保是正数。他们在合约里会约定，先要发工人工资。不相信吗？去看看倒闭的工厂、破产的企业吧，大浪淘沙，数不胜数。 <br />
要去掉有色眼镜，彻底远离马克思关于资本、关于剥削等理论的污染，才会真正懂得一般化资本理论，货币是资本，劳动也是资本。出卖劳动换取报酬的工人，当然也可以说是资本家。 <br />
一般化了资本的概念，才会明白，所谓企业本质是一张合约，不同类别的资本，劳动力、货币、管理能力、经验技术、房屋建筑和机械设备的组合合约。企业通过政府之力，取得垄断地位，排挤竞争对手；工人通过工会，在政府支持下罢工，打击那些愿意上班的员工，这些都是反市场的行为。最终的结果，是他们取得额外的租值回报，而受损的是更多的企业，更多的员工，以及所有的消费者。 <br />
故此，那些持有“工人相对企业，是弱势地位，故此工会有助改善这种境况”观点的人，其在其他方面的观点必然会有相同的隐忧。他们同样可以说“企业做大了，消费者是弱势，因此反垄断能改善消费者境况“。 <br />
错误的观点是一连串的事，不会孤立。</div>

							
							
							
							
