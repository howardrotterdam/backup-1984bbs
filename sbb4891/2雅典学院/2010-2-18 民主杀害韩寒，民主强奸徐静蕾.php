													<h2>一元民主？</h2>
						
						
																											<div id="postmessage_483587" class="t_msgfont">我想谁都知道人们谈论的民主宪政不是这种＂纯粹的民主＂吧？<br />
<br />
还有就是＂民主＂不能破坏法治啊？民主法治自由三者可不是互为破坏的关系啊？<br />
<br />
又杀又奸的什么玩意？<br />
<br />
＋＋＋＋＋＋＋＋＋＋＋＋＋＋＋＋＋＋＋＋＋＋＋＋＋＋＋＋＋＋＋＋＋＋＋＋＋＋＋＋＋＋＋＋＋＋＋＋＋＋＋＋＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝<br />
<br />
转一文：<br />
<br />
民主不只是投票。 <br />
<br />
&nbsp; &nbsp; 首先，民主是一种生活方式，价值观念，投票或票决是民主的外在表现方式之一，而且是重要的表现方式之一，但不是全部； <br />
<br />
&nbsp; &nbsp; 第二，民主这种生活方式需要制度的有效保证和保护，即民主政体的建立――民主政体即国家的制衡机制，以保证民主选举的公开、公平和公正。 <br />
<br />
&nbsp; &nbsp; 多说几句，民主政体的要件： <br />
<br />
&nbsp; &nbsp; 1、立法、司法、行政独立，军队国家化（即中立，任何党派不能利用国家军队达成自己的目的），宪法由在政府解散状态下成立的全国国民制宪大会制定然后根据此宪法选举产生政府，而非由政府制定宪法； <br />
<br />
&nbsp; &nbsp; 2、言论、新闻、出版独立而自由（必须是私立，政府办媒体是违法的）； <br />
<br />
&nbsp; &nbsp; 3、各政党不能在基层建立组织，只能在市以上建立分部，基层支部是非法的（这一点很重要，目的是防止各党派控制各党成员的独立思想，影响选举的公正，即公民个人意愿的自由表达）； <br />
<br />
&nbsp; &nbsp; 4、教育中立化，任何党派和利益集团都不得干预； <br />
<br />
&nbsp; &nbsp; 5、政教分离和信仰自由。政教分离就是把利益和信仰分离开来，这样一来，不同利益集团的人就可以信仰同一种宗教，而不同的教派之间也能和睦相处、相安无事。 宗教参与政治必定会导致动乱和流血。这是中东问题的根源。<br />
 <br />
&nbsp; &nbsp; 一个国家的长期繁荣和昌盛必须靠民主和自由来共同支撑：民主确保的是国家事务的公开、公平和公正；自由确保的是个人个性的完美发展和创造性的充分发挥。 <br />
<br />
&nbsp; &nbsp; 可以说，民主和自由是一个国家繁荣进步的两个发动机。 <br />
<br />
&nbsp; &nbsp; 同样，一个社会的长期稳定与祥和必须靠法制和宗教来共同支撑：法制是强制性的，宗教是自律性的――法律管行为，宗教管心灵、管道德。可以这么说，法制和宗教就象一驾马车的两个轮子，缺一不可。因为我们的生活不可能事事使用法律手段，只有自律行不通的地方才会用到法律。 <br />
<br />
&nbsp; &nbsp; 可以这么说，宗教和法制是一个社会长期稳定祥和的两个平衡器。 <br />
<br />
&nbsp; &nbsp; 由此可见，一个正常的社会是不能没有法制、也不能没有宗教的，就象一个正常的国家不能没有民主和自由一样。我国的民风日下、道德普遍滑落，在很大程度上与宗教的缺失和法制的缺位有着直接的关系。<br />
