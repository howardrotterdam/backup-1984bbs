																											<div id="postmessage_457071" class="t_msgfont">孔丘其人<br />
北京大学、清华大学大批判组 (1974.04.03) <br />
《人民日报》<br />
　　孔丘其人，顽固凶狠却又十分虚弱，阴险狡诈而又极端腐朽。这是他所代表的没落奴隶主阶级的本性，也是一切行将灭亡的反动阶级代表人物的共同特点。今天，彻底揭露孔丘的反动面目，对于识别王明、刘少奇、林彪这一类政治骗子，反击开倒车、搞复辟的逆流，很有意义。<br />
<br />
　　开历史倒车的复辟狂<br />
　　孔丘（公元前五五一――四七九年）生活的春秋末期，奴隶起义、平民暴动、新兴地主阶级的夺权斗争，汇成了一股不可阻挡的历史潮流，把腐朽的奴隶制度冲得“礼坏乐崩”，使整个奴隶主阶级面临灭顶之灾。新兴的地主阶级正在剧烈的阶级斗争中占领历史舞台。<br />
　　孔丘出身的没落奴隶主贵族家庭，在这个社会大变革中急剧地衰落下来。他的祖先原是宋国的大贵族，后来搬到鲁国。到他父亲Y叔纥（Y音邹，纥音合）一死，孔家就更加日益没落。孔老二从小接受奴隶主阶级的反动教育，年轻时就走上了维护和复辟奴隶制的反动道路。阶级的衰败和家庭的没落，更使他顽固地、狂热地为恢复被夺去的“天堂”而斗争。“兴灭国，继绝世，举逸民”，就是孔丘终生的复辟梦想。它的集中表现，便是一条“克己复礼”的反动政治路线。<br />
　　孔丘这个家伙对于往日奴隶主阶级的旧制度、旧秩序、旧文化十分怀念，对于当时大好的革命形势刻骨仇恨，满脑子反革命复仇思想。看到烽烟滚滚的奴隶起义，孔老二破口大骂是“强盗”和“祸害”，恨不得斩尽杀绝。郑国奴隶主在一次镇压奴隶起义时，杀害了全部参加起义的奴隶，他听到后狂叫“杀得好！”（《左传&#8226;昭公二十年》）孔丘的“克己复礼”，就是奴隶主屠杀有理，奴隶造反无理，他完全是奴隶解放的死对头！<br />
　　孔丘如此仇视新事物，以种种努力保持旧事物使它免于死亡，简直达到了发狂的地步。孔老二这个“复礼”狂，正是一个开历史倒车的复辟狂！<br />
<br />
　　虚伪狡猾的政治骗子<br />
　　政治骗子都很注意窥测方向，见风使舵，经常涂改自己的脸谱。用孔丘自己的话说，就是“邦有道，危言危行；邦无道，危行言孙。”（《论语&#8226;宪问》）这就是说，国家形势对复辟有利时，就大喊大叫，赤膊上阵；形势不利时，还要坚持复辟活动，但表面上要说好话，装笑脸，不露马脚。公元前五○一年，鲁国发生了一次政变。公山不狃以费城为据点，准备推翻新兴地主阶级季桓子等人的统治。公山不狃派人与孔老二联络。一心要复辟变天的孔老二欣喜若狂，手舞足蹈，企图利用这次政变，从费城起家，干一番“复礼”的大事业，大叫要“使周代的制度在东方复兴”（《史记&#8226;孔子世家》），抛出了一个反革命复辟的行动纲领。但是，这次政变很快就失败了。孔丘贼心不死，摇身一变，隐瞒了自己要参与政变的真象，还说了一个政变头目的坏话，蒙蔽了季桓子，骗取了信任，居然当上鲁国首都行政长官，并很快窃取了警察总监和代理宰相的要职。就这样，孔丘这个政治骗子改用“钻进去”的战术，篡夺了鲁国的一部分权力。<br />
　　 <br />
&nbsp; &nbsp; 凶狠残暴的大恶霸<br />
　　一朝权在手，便把令来行。孔丘一上台，就立刻把复辟希望变成复辟行动，疯狂地推行他的反动政治路线，残酷地镇压劳动人民和新兴地主阶级，特别把广大妇女压在社会最底层。<br />
　　孔丘这个公开申明搞政治根本不需要杀人的伪君子，稍一得势，就露出了一副大恶霸的凶残面目。他当代理宰相仅三个月，就对新兴地主阶级革新派少正卯下了毒手，杀了之后，还下令把尸体示众三天。<br />
　　列宁说：“什么叫做复辟？复辟就是国家政权落到旧制度的政治代表手里。”（《社会民主党在俄国第一次革命中的土地纲领》）孔丘的“克己复礼”，就是复辟奴隶主阶级法西斯式的专政。孔丘杀害少正卯这一事件告诉我们，两个阶级、两条政治路线的斗争是生死斗争。在阶级社会里，根本没有所谓超阶级的“仁政”。代表反动阶级的复辟势力一旦上台，就会象孔丘那样疯狂地进行反攻倒算，革命者就会人头落地。这是一个值得记取的历史教训。<br />
　　 <br />
&nbsp; &nbsp; 不学无术的寄生虫<br />
　　孔丘从维护和复辟奴隶制的政治需要出发，把自己打扮成一个集中了西周文化的天生“圣人”、道貌岸然的正人君子，欺世惑众。后代的反动派同样是为了维护和复辟旧制度的需要，也把孔老二捧为“大学问家”。这个纸老虎，必须彻底戳穿。<br />
　　孔丘是什么“大学问家”吗？完全是骗人的鬼话！<br />
　　毛主席指出：“自从有阶级的社会存在以来，世界上的知识只有两门，一门叫做生产斗争知识，一门叫做阶级斗争知识。”（《整顿党的作风》）孔老二这个家伙，一不懂革命理论，二不会生产劳动，根本没有什么真才实学，而是一个满肚子秕糠的大草包。当时劳动人民就骂他“四体不勤，五谷不分”。他的生产知识等于零。他向学生灌输的所谓礼乐仁义，也完全是奴隶主贵族僵死的旧文化。他修的《春秋》，更是颠倒历史，混淆黑白，是一本无耻美化奴隶主头子、恶毒攻击革新势力的变天账。<br />
　　春秋战国的诸子百家当中，有不少人写了专门著作，总结了当时阶级斗争、生产斗争某一方面的经验，在认识史上有所贡献。特别是法家的一些优秀代表，如商鞅、荀子、韩非子等人，更是站在时代潮流的前面，阐发了革新的思想，丰富了我国古代灿烂的文化。而孔老二这个家伙却是“述而不作”，根本写不出什么东西。儒家经典《论语》也不是孔丘的亲笔，而是他生前的一些言行，由他的徒子徒孙回忆记录下来，拼凑而成，鸡零狗碎，杂乱无章。查其内容，更是反动腐朽。不是鼓吹“天命”论，就是大念复辟经，再有便是如何搞阴谋、耍两面派以及腐朽的生活方式和处世哲学。什么“粮食越精越好，肉切得越细越好”，“烹调不到家，不吃。肉切得不方正，不吃”，“穿黑色的羊羔皮袍，一定配上黑色的罩衣；穿白色的鹿皮袍，一定配上白色的罩衣；穿黄色的狐皮袍，一定配上黄色的罩衣”；什么对国君如何毕恭毕敬，对权臣如何笑脸相迎，在大庭广众之中如何装出一副忠厚老实的面孔，在社会上如何骗取正人君子的美名。（《论语&#8226;乡党》）看，乌七八糟，腐败不堪，这就是儒家的经典！卑鄙无耻，百般钻营，这就是孔丘的学问！明代的进步思想家李贽一针见血地指出孔丘“无学无术”。（《焚书&#8226;答耿中丞》）连德国资产阶级唯心主义哲学家黑格尔也瞧不起孔丘那一套，说在孔丘那里根本没有什么真正的哲学，还说：“为了保持孔子的名声，假使他的书从来不曾有过翻译，那倒是更好的事。”（《哲学史讲演录》）这对于孔老二这个不学无术的寄生虫，倒是一个绝妙的讽刺。<br />
　　 <br />
&nbsp; &nbsp; 到处碰壁的丧家狗<br />
　　毛主席说：“凡属倒退行为，结果都和主持者的原来的愿望相反。”（《新民主主义的宪政》）孔丘一生，到处搞复辟，到处碰壁。当时人民群众骂孔老二“累累若丧家之狗”，这句话生动地概括了孔老二一生的遭遇。这是他推行那条倒行逆施的政治路线的必然结果。<br />
　　孔老二打着“克己复礼”的破旗，坐着一辆马车，颠颠簸簸，先后到过齐、卫、宋、陈、蔡、楚等国，奔波了十几年。但所到之处，不是坐冷板凳，就是被驱逐出境，大家都厌恶这个死顽固。卫国国君听说孔丘善于搞阴谋，派人带着武器出出进进地盯着他；齐国和宋国的新兴势力还要杀掉他。孔老二每次都夹着尾巴狼狈逃窜。<br />
　　孔丘到处碰壁，连他的弟子们也纷纷离开了他。孔老二哀叹：“如果我的主张行不通，我要坐着木筏到海外去。能跟随我的人，大概只有子路了。”（《论语&#8226;公冶长》）但就是这个子路，也在一次维护奴隶制的战斗中被人砍成肉酱，成了奴隶制度的殉葬品。孔丘如果还想坐着木筏亡命海外，就连一个追随的人也没有了。到处碰壁，众叛亲离，这就是一切搞复辟的反动头子的必然下场。<br />
　　马克思说：“让死人去埋葬和痛哭自己的尸体吧。最先朝气蓬勃地投入新生活的人，他们的命运是令人羡慕的。”（《摘自“德法年鉴”的书信》）死抱住孔老二僵尸不放、妄图开历史倒车的人，他们的前途是不美妙的。世界的未来属于无产者！　　<br />
