																											<div id="postmessage_631471" class="t_msgfont">码字之前打一句废话 我是我们班唯一一个在系主任开会要求每个人都交入党申请书的条件下没交入党申请书的人 所以想怀疑我立场并以此来喷我的人你们可以退散了<br />
先引一段话<br />
“你们连党都入了,还有什么恶心的事情干不出来.为了工作,为了考研,为了父母,被逼的,被操的.无数个理由,无数个委屈,像新婚的媳妇被山大王抓去做七姨太一样,一哭,二闹,三上吊.想入党那是你的事情,想考公务员那也是你的事情.别他妈拿出来丢人现眼.要懂得做人,虽然你们不是人,但要会夹着尾巴做人嘛. 别拿着鸡毛当令箭,更别拿着大尾巴再咱面前晃悠.”<br />
<br />
这段话基本上包含了两个意思<br />
第一入党=投机=无节操=下贱=你要敢说出来你入党你就是恬不知耻臭不要脸自寻死路（为了工作,为了考研,为了父母,被逼的,被操的.无数个理由,无数个委屈,像新婚的媳妇被山大王抓去做七姨太一样,要会夹着尾巴做人嘛.别拿着鸡毛当令箭,更别拿着大尾巴再咱面前晃悠.）<br />
第二 党是恶党→凡是加入他的都是恶人（你们连党都入了,还有什么恶心的事情干不出来.要懂得做人,虽然你们不是人 还有就是共产党员都是罪犯）<br />
基本上见党员就喷的都是上面那两种想法吧<br />
先说第一个吧<br />
投机这个事各位有谁没干过么？或者说 各位有谁干得少么？你考试从不作弊么？你从来都没有依靠人际关系获得本来不该属于你的东西或者逃避惩罚么？在人治社会中 我们每一个人都是投机者 并且我们这些投机者大部分都曾经或正在或将要恬不知耻臭不要脸的向别人炫耀自己的投机，并且恬不知耻臭不要脸的教育别人如何投机 有人该说了 即使是投机入党和考试作弊也是程度不同不能同一而论 没错 也许大家认为这是程度不同的事 所以大家只不过是五十步笑百步而已 如果对方的灵魂是卖给了TG 那么我们大多数人的灵魂也都卖给了魔鬼 只是卖的多与少以及贵与贱的问题<br />
所以我觉得 我们并不比那些为了获取一点利益入党的P民高尚多少 诚然 为了获取一点利益就入了党并不是什么值得炫耀的事 但是我们大概也并没有什么立场来指责他们 有资格朝着罪人扔石头的人是不存在的<br />
再说第二个吧<br />
从回帖上看 貌似抱着第二个想法的人极为稀有 这在我看来应该算是不幸中的大幸如果大家都是抱着第二种想法来喷党员的话那就悲哀了（最悲哀的就是以为持反对意见的人只是“为党员身份以及组织辩护” 好歹第二种想法还有一个界限人家只是把党员给那啥了 好歹没把自己这边的人给踹过去 大脑还保持着最低程度的机能）<br />
这是典型的文革遗产 想想过去我们是怎么打地主打右派的想想我们是怎么对待地主和右派的子女的 想想当年一个我们并不了解但却有地主身份的人站在我们面前时 我们是怎么抱着怎样的想法看待他的在对比一下“你们连党都入了,还有什么恶心的事情干不出来”这句话 不知诸位作何感想<br />
<br />
最后在说两句我对党员的看法以及由此衍生出来的想法吧<br />
在天朝出生的人其实都不是作为人出生的 我个人觉得 朝廷只不过是把新生儿当成刚刚制造好的工具看待而已 我们从小就被教育“我是一块砖哪里需要往哪搬” “为了保护国家财产而牺牲生命” “顾全大局牺牲个人利益” 再往前推个二三十年还有“爹亲娘亲不如毛主席亲”这样的话我们在刚刚生下来的时候就被教育成“一块砖” 我们与生俱来的宽容 同情 对他人的尊重 与他人的羁绊 守护最重要的人的心情这些属于人的感情都被剥夺了 所以我们不宽容 我们不同情 我们不尊重 我们冷漠 我们不把别人当人看 就像当年反右反地主那段时期我们认为地主这个集团内的人 不会有好人 因为我们不把这些人当成人来看 只是把他们当成一个零件来看 当一个我们并不了解的被称为地主阶级的人站在我们面前时 我们发自内心的愤怒 发自内心的仇视他 我们认为他一定恶贯满盈虽然我们不知道他干了什么就像我们认为用一个坏了的模具制造出来的零件一定是个次品一样 我们不认为他还有人的品质 所以我们不把他当人看我们认为决定一个人的最主要因素是他所属的集团或者立场 就像决定一个零件好坏的因素是制作他的模具的好坏一样 以上几种因素使得我们看上去缺了点人味&nbsp;&nbsp;也正因为我们缺了点人味 所以这个群体出现了种种不好的现象（甚至我们当中的一部分人把这作为我们劣等的证据） 于是这个社会就成了非人的社会<br />
现在这个社会的一部分成员开始进化 他们不再想作为一个零件而是想以一个人的身份活下去 昨天跟人聊天时还提到 这个种族的希望到底在哪 我个人认为当这个种族开始追求人这个身份的时候 希望就出现了 如果他们希望能以人的身份生活的话 他们必将懂得什么是尊重和宽容 什么是平等和自由这些理念将不会仅仅停留在口头上 而是像种子一样扎根在每个人的灵魂深处 当这粒种子开花结果之时 就是奇迹降临之日<br />
但是目前这个时期离奇迹之类的东西还嘛大嘛大呢 虽然有很多人开始拥有了追求“人”这个身份的意识 但他们离一个真正的人还差得远 他们仍然不宽容 不理解 不尊重 最重要的是 他们仍然不知道把其他人看成人而不是由某个模具制造出来的零件 比如之前某人曾说“我认为入党的都是怎么怎么样的” 再比如喷党员的各位 比如KAIKAI吧&nbsp;&nbsp;按他说的 假如有那么一个人 他是党员 但你压根就不了解他 这个时候这个党员说了我是党员 KAIKAI会怎么认为呢？根据引用的那一段话 他会认为你们不是人 别拿着大尾巴再咱面前晃悠 我估计有这种想法的人不在少数吧？那么我就想问了 你怎么知道他不是人?你都不知道他曾经做过什么 他的为人如何 你都不了解他不认识他 你怎么就能断定他不是人？你对他的嗔恨心和负面评价从哪来的？实际上 这种嗔恨心的根源仍然是模具 也就算TG 你之所以会把对TG的愤怒转嫁到这个人身上 正是因为你根本就忽略了这个人本身所可能具有的人性 在党性面前 人性对你来说是忽略不计的 正如在你对这个人的判断中压根就无视了人性这个因素一样 你还是把他当成零件来看而不是人 党性高于人性这句话已经深深的影响了你的思维 从这个角度来讲 各位都是党员 各位都是零件<br />
所以我认为 在评判党员这个问题上 应该抛开党员这个身份 单纯的以一个人的行为来判断他 就算是他为了投机而入党 也只是把他看成一种我们都做过的投机行为 而不是因为他加入的是TG就变成了某种更加不可饶恕的投机倒把 把我们视野中的党员还原成人 把标签去掉 把他们当人来看而不是一个脸谱化的零件 THAT'S ALL<br />
