																											<div id="postmessage_673590" class="t_msgfont">莫名，我就仇恨你<br />
from 韩寒 by 韩寒<br />
100+ people liked this<br />
<br />
我最近经常在论坛里看到69圣战，当时我并不了解内容，很明显，我对69两个字的好感要大于圣战两个字，我挺害怕看见“圣战”两字的，就像我害怕看见“坚决打倒”“旗帜鲜明”等带有恐怖色彩的词汇一样，所有的这些词汇都代表着民间狂热煽动和失去理智，官方完全排他和铲除异己。经过了大致了解，发现这好在只是一场大学生欺负初中生事件，说实话，我为两方都觉得挺惭愧的。<br />
<br />
我与韩国人的唯一接触是五年前在韩国一支车队比赛，参加亚洲的一个方程式锦标赛，我的队友是一个韩国车手，家境非常一般，以前在这个车队做维修工，后来变成了韩国很不错的赛车手，算是一个励志故事。那个时候无论我去韩国比赛或者他们来中国比赛，都觉得挺融洽。<br />
<br />
突然之间，中国的年轻人，尤其是网民对韩国人非常反感，其反感程度大大超过了日本，说实话我点我觉得挺奇怪的。后来开始传出韩国人掠夺中国文化遗产的事情，我本人作为非物质文化遗产也被抢过一次。好在新中国几乎没有留下什么文化遗产，所以也避免了现代文化遗产被别的国家抢去的恶果，于是盛传韩国人要抢我们古代的文化遗产，从四大发明到文人墨客，都成为韩国人争相论证有韩国血统的对象，我中华民族文化人上下五千年来几乎从来没有过可以随心所欲写文章的时候，基本上还没写出代表人类进步的东西来，身体器官就会缺少一点什么，大则脑袋小则鸟，写点前朝的事已经算是最大尺度了，所以留下的真正文化遗产屈指可数，我们都是很宝贝的，你随便抢走一个，我们就损失了百分之二十五啊，大家的激动我很能理解，如果我们国家哪天宣布莎士比亚，伏尔泰，高尔基，舒伯特，但丁，雨果，海明威，川端康成均拥有中国血统，我估计八国联军得再出动，一个国家的文化是一个成熟国民对自己国家自豪感的重大来源。偏偏韩国人最喜欢抢中国文化。<br />
<br />
但这是真的么，这些都不是真的，除了端午节和韩国的端午祭有名字上的冲突以外，其他的所有有关韩国掠夺我们文化遗产的事件都是我们自己捏造或者夸大的，这事情说出来其实挺难接受的，但事实就是如此，我不喜欢你，于是我编造了你来我家偷东西的故事，并且意淫了没偷着被我家的狗给咬了的结局。<br />
<br />
 <br />
<br />
我喜欢韩国么？说实话，我并不喜欢韩国，我的生活里几乎没有什么韩国的产品，韩国的电器在中国卖很贵，性价比不高，韩国车进步很大，但始终不能算作一流，我肯定不会购买，看过几部韩国电影，有两三部很不错，其他也都一般，那些不错的电影也普遍压抑，我绝对不会看韩剧，我绝对不会听韩国歌星唱歌，也不喜欢他们的打扮，我也不喜欢韩国料理――但我绝不讨厌韩国，我甚至尊重韩国。如果你了解一些韩国的民主进程，你应当敬佩韩国人民。韩国虽然国土面积不大，但是他们居然向世界的部分，全亚洲，尤其是中国输出了他们当代的文化。你不管这个文化深刻还是肤浅，韩国做到了，韩国还输出了他的自主商品品牌，而且还都不低端。<br />
<br />
 <br />
<br />
中国和韩国官方之间始终没有大的敌意，我不明白为什么两国的年轻人会有那么大的敌意，当然，主要其实还是我国不同年龄段网民之间的互掐。我们何必要这些莫名其妙的仇恨和对立。如果有商家在背后做推手，那就更不应该挑起这些不同民族不同时代的年轻人之间的矛盾。中国人，韩国人，韩国年轻人，中国八零后，中国九零后，我们其实应该坐在一起点着篝火好好聊天才对，你握握我们的手，我握握你们的手，你泡泡我们的妞，我泡泡你们的妞，多么和谐，当然，如果是你泡泡你们的妞，我泡泡你们的妞，那就更和谐。<br />
<br />
 <br />
<br />
我一直不知道中国的大孩子们在自豪些什么，我知道小时候我自豪是因为老师老说中国大，只有加拿大和苏联比中国大，后来苏联解题，我很高兴，因为这下苏联没中国大了吧，结果俄罗斯还是比中国大，我很伤心，我经常看着澳大利亚和美国庆幸，庆幸他们没有再大一点，否则就比我们还大了，我也经常看着印度和蒙古神伤，觉得如果这两个是我们的，那我们就更大。后来等知道了，什么才是一个国家真正值得骄傲和荣耀的东西以后，我再也不嘲笑别的国家小。看到中国人疯狂去国外够买奢侈品的时候我很伤感，看到劳斯莱斯和法拉利宣布中国市场成为亚洲第一的时候我很伤感，但这些也许是很多人自豪感的来源，尽管其实他们廉价的劳动为他们老板的劳斯莱斯奉献了一颗螺丝，他们的土地和房子为当地官员太太的路易威登奉献了一个扣子，哦，对不起，他们从来没有土地，但是他们依然很自豪，因为他的老板，他们的官员，他们的太太二奶和孩子都是中国人，哦，对不起，孩子很可能已经不是中国籍，我理解这种自豪，不过我挺自卑的，因为我以文化而谋生，当我和别的国家的文化人在一起的时候，我没有办法和他们吹牛逼，我也不能列举出中国的媒体，音乐，文学，影视，服装，美术，哲学对世界的影响，哪怕来点所谓的流行文化，让韩国人和日本人来哈哈咱们，也一个都说不上来。到头来我也只能说，你别气我，小心我们当官的儿子去你们国家买光你们的法拉利，让你们的艺术家都买不到。<br />
<br />
 <br />
<br />
在这个大朋友欺负小朋友的游戏里，大朋友们其实应该再想的多一点，多替你们的小妹妹小弟弟想想，他们傻么，他们是有点傻，而我想告诉那些小弟弟小妹妹们，每个人都有自己的偶像，甭管他是哪个国家的，等大家长大了以后，有些人觉得自己真有眼光，偶像自己也很争气，你说出自己的偶像的时周围人都觉得你有品，你可以继续粉他，你可以不粉他，但你无悔自己年少时的选择，在你的一生里，你都可以大声的说，我的偶像是叉叉叉。而有些人长大以后都不好意思告诉别人自己年少的时候粉的是叉叉叉，恨不得把当时的日记和照片都撕了，别人帮你回忆起来，你还不愿意承认。小心成为后者。我不是假装过来人在和你装逼似的传授人生经验，那是怪叔叔，你要小心的，我是另一个大朋友。<br />
