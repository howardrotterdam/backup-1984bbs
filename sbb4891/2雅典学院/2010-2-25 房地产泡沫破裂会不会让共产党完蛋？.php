													<h2>房地产泡沫破裂会不会让共产党完蛋？</h2>
						
						
																											<div id="postmessage_492899" class="t_msgfont">银行业有这样一个玩笑说：如果你欠银行10万元，那银行逼债会逼死你；如果你欠银行10个亿，那当你出了问题，银行会派保镖日夜陪伴你，生怕你死了、跑了、不还账了，甚至还可能借给你更多的钱，帮你度过难关。这个笑话很好地说明了中国开发商和银行之间的关系。<br />
<br />
&nbsp; &nbsp; 在中国，欠银行的钱能以十亿为单位的，也只有那些房地产商和国企了。私企不要说欠银行十亿，就算想欠银行一百万都很难。金融本来该起到配置资源的作用，把钱贷给那些有发展前途，能提供大量就业机会，对宏观经济有促进作用的企业。可在中国受权力支配的扭曲市场经济的大环境下，能得到贷款的往往是那些效率低下，解决不了多少就业，能给政府官员带来中饱私囊机会的开发商和国企。2009年接近十万亿的天量信贷，绝大多数流向了国企，而拿到贷款的国企不去投资实体经济，却把钱用来炒地皮做开发商间接推高房价就证明了这点。&nbsp;&nbsp;<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;中国的房地产和政府的利益关联人尽皆知。1994年分税制改革后，地方政府的财政就一直吃紧。地方政府赤字惊人就想到了土地财政。高房价对地方政府是很有好处的。房价涨了，土地出让金和房地产相关税收会增加，地方政府就有钱搞基建了，比如修高架地铁和大桥，这些东西造好后，会带动周边的房价上涨，比如上海的地铁推高了地铁站周边的房价，崇明跨海大桥修好后，崇明的房价也上涨。房价一涨，政府的土地出让金和相关税后会更多，就有更多的钱搞基建了，所以这对政府来说是一个良性循环。而且房价上涨时，银行贷给开发商炒房客和房奴的贷款也能收回来，如果房价下跌的话，首先开发商拿地不积极，政府的土地出让金会减少，没钱搞基建，GDP保八的目标也完不成。中国人普遍有买涨不买跌的心态，房价涨的时候抢着买，生怕再涨就买不起了。而房价下跌的时候，大家普遍持观望的态度，认为房价还会再跌别急着买。房价下跌时没人买房子，开发商还不了欠银行的贷款，一些房奴也会选择断供，这都会威胁银行的金融安全。<br />
<br />
&nbsp; &nbsp; 中国政府严格限制民间资本进入金融业，中国绝大多数的银行都是国有，这意味着就算房价下跌导致银行坏账堆积如山中国政府也不可能让银行破产。国有银行是建立在政府信用上的，国有银行破产就是政府信用破产，这会严重威胁到社会稳定。所以如果国有银行出了问题，中国政府一定会动用国家财政来救他们。&nbsp; &nbsp; <br />
 <br />
&nbsp; &nbsp; 国有银行大的问题是产权模糊。国有独资银行虽然说名义属于全民所有，但它的控制权几乎全部落在代理，也就是银行官员的手中。而那些银行官员手中的权力缺乏有效制约，他们可以慨纳税人之慷，不负责任地给利益关联人及半死不活的国企贷款。有时国有银行官员放贷完全变成了一件“政治任务”，是为了自己的政治前途着想，而不是为了银行的资产安全着想。银行官员长期以来不负责任的放贷造成了国有银行的天量坏账，资产质量极低。如果按照国际标准，中国一些国有银行早不知道破产多少回了。可是中国的国情是政府的权力不受制约，可以随意动用国库的钱来为不负责任的银行官员买单。1998年财政部对四大行2700亿元的注资， 1999年剥离 的1.4万亿元不良资产（相当于每个中国人头上分担了一万多的不良资产），2003将450亿美元的外汇储备注资国有商业银行。除此之外，政府还通过让银行上市融资来减少坏账，四大行中除了农业银行坏账太多，实在没法上市，其余的三大行都以极高发行价上市，轻而易举地圈走了二级市场广大散户的血汗钱。可这样动用纳税人的血汗钱来救国有银行，非但没减少其坏账的数量，反而进一步助长了银行官员有恃无恐地乱放贷款，反正政府不会让我们破产，最后有纳税人这个冤大头来买单，我们还怕什么？2006年中国四大行当年未剥离的不良资产加上新增加的不良资产上升至2万亿。如果中国的房地产泡沫破裂，2009年银行放出的接近十万亿的天量信贷不知道有多少会变成收不回来的烂帐。<br />
<br />
&nbsp;&nbsp;在建国后最大的金融诈骗案“德隆系”中，德隆集团董事局主席唐万里曾对前来要账的国有银行经理说：“德隆现在不但无钱可还，还需要向银行再贷款几千万。否则大家一起完蛋。”而在2008年的房价下跌中，地产大鳄任志强也喊出了“要死也是银行先死。”这两人的语言有异曲同工之妙，因为他们都绑架了银行，或者说银行自愿被他们绑架了。德隆案发后，根据央行的估计,德隆系造成金融机构的实际损失总额超过200亿元。如果中国的房地产泡沫破裂，给金融行业带来的损失将远远不止这个数字。因为中国只有一个德隆，却有无数向银行贷款的房地产商和炒房客，与德隆系唐万里唐万新兄弟相比，中国的房地产利益集团对银行的绑架程度要高的多。他们是更高明更疯狂的绑匪。&nbsp;&nbsp;<br />
<br />
&nbsp; &nbsp;除了上市，银行注资外，还有一个办法能减少国有银行的不良资产，就是开印钞机用通货膨胀让人民币对内贬值。人民币不像美元是国际货币，美国政府开印钞机，可以在全世界透支购买力，损害的是全世界美元持有者的利益，也包括持有2.4万亿美元外汇储备的中国的利益。中国政府开印钞机，损害的只是手头持有人民币的中国人的利益。<br />
<br />
&nbsp; &nbsp;开印钞机这个屡试不爽的办法政府早就开始用了，政府钞票印多了，老百姓手头的钱购买力自然会下降。经济学家金岩石说，从1992年到现在，央行每年印刷纸币的速度在23%左右，而经济增长速度却在8.9%左右，按照经济学意义上的通货膨胀计算方法，通胀就在14%左右，以复利计算，也就是5年要翻一番，也就是说每五年人民币的购买力要打对折。我认为金岩石的判断是对的，以黄金为参照物，五年前的2005年，110元人民币可以买一克黄金，而现在240元人民币才能买一克黄金。和黄金比，人民币五年内贬值了55%。五年前的2005年，上海商品房均价6698元一平方，而眼下上海商品房均价15800元一平方，和上海房价比，人民币五年内贬值了58%。<br />
<br />
