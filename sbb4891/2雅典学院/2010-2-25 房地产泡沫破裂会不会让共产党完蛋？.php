　　对于资产泡沫，索罗斯给出的则是一个对冲基金式的回答，“当看到资产有泡沫现象时，可以趁机买货，因为这是市场大方向，不算非理性行为，但当泡沫成熟时，就要及时沽货获利。”<br />
<br />
　　2月4日清晨，在其下榻的港岛香格里拉酒店56楼，索罗斯接受了本刊记者的专访，畅谈其对全球及中国经济、资本市场的看法。年近80岁的索罗斯，身着浅灰色毛衣、米色休闲裤，尽管精神依然健硕，但仍难与动如脱兔的资本大鳄联系起来。<br />
<br />
警惕“硬着陆”<br />
<br />
　　《新世纪》：你在数天前指出，全球资产泡沫正在形成。考虑到去年初至今，不少游资流入中国，以及中国持续推出宽松货币政策和经济刺激方案，你认为中国是否已经出现了资产泡沫？<br />
<br />
　　索罗斯：毫无疑问，中国银行业的放贷是过度的，中国政府已经采取适当的措施对其进行了限制，这势必对股市产生影响。除非宏观调控政策达到了令经济降温的效果，否则股市将一直受到相关压制。<br />
<br />
　　至于泡沫则很难判断。人们总是认为泡沫无处不在。刺激政策毫无疑问已经推高了资产价格，但要判断泡沫存在与否，很大程度上要取决于未来经济的走向是“硬着陆”抑或“软着陆”。如果经济实现了“软着陆”，就说明目前没有泡沫，反之亦然。所以，如果想知道现在是否存在泡沫，只有耐心等待。<br />
<br />
　　《新世纪》：那么目前的调控措施，将会带来经济“硬着陆”还是“软着陆”？<br />
<br />
　　索罗斯：这是个复杂的问题。首先，在政府刚开始限制信贷的时候，信贷需求反而会出现井喷。出于对未来继续收紧的预期，人们会争先恐后，贷到多少是多少。因此，信贷紧缩政策需要一段时间才能真正使经济降温。这段时间越长，经济“硬着陆”的风险就越大。<br />
<br />
　　如果经济在明年以前持续加速增长，供给就会剧烈放大，需求却相对收缩，引起更加严重的产能过剩问题，继而导致经济“硬着陆”。所以，我认为政府当前尽力控制信贷的举措是警觉有加的。<br />
<br />
　　《新世纪》：中国银行业2009年新增贷款10万亿元，今年政府公开表示约为7.5万亿元。接下来会否导致过热、通胀以及更加激烈的紧缩政策？中长期而言，你担心中国银行体系的不良贷款风险吗？<br />
<br />
　　索罗斯：经济过热、通胀等现象正在中国发生，而且这种现象将会持续，直到经济降温为止。信贷爆炸在未来的某一时候注定会产生坏账，但同样，这主要取决于经济走向“硬着陆”还是“软着陆”。<br />
<br />
　　《新世纪》：最近一段时间以来，不少国际投资者对中国的态度都发生了微调，由此前的极度乐观或乐观，转向“中性”或“稍微谨慎”。你对中国的态度现在是怎样的？<br />
<br />
　　索罗斯：我是保持非常谨慎（very cautious）的态度。如果中国经济冷却一些，我才可能变得再次乐观。<br />
<br />
　　《新世纪》：“退出政策”是近来全球投资界最关注的话题。中国人民银行近期提高了银行法定存款准备金率，这项举措较市场预期为早，你如何评价这项政策，这是否意味着“紧缩”周期的开始？<br />
<br />
　　 索罗斯：政府提高法定存款准备金率是非常有力之举，我甚至可以说，中国当局比市场反应还机警。<br />
<br />
　　《新世纪》：中国的房地产价格在去年底急剧上升，政府近期也在出台收紧的措施。但也有人认为，中国的房地产价格很难控制，这主要是由于内地正在进行的城镇化、国际资本的投资需求以及正在崛起的中产阶级的消费升级需求所致。你对中国房地产中长期的看法如何？<br />
<br />
　　索罗斯：住宅房地产市场增长过快，一定是周期性的，因此会有调整空间。不过中长期可能仍保持快速增长。<br />
<br />
　　《新世纪》：你曾多次呼吁人民币升值，这样不仅可以解决全球贸易不平衡的问题，而且可以帮助解决中国的通胀问题。但是，如果人民币单边升值，政府如何处理“热钱”流入问题？<br />
<br />
　　索罗斯：无论从国内抑或国际角度分析，我认为中国政府如果允许人民币升值，都将是明智之举。<br />
<br />
　　如何避免“热钱”流入是一个大问题。其中一种解决方式是一次性升值，然后再引入爬行区间（Crawling Band）浮动汇率制度。这会使得人民币既有上升也有下降可能，将可借此打击投机。<br />
<br />
　　不过，我判断中国政府不大可能这么做，因为他们非常谨慎，不喜欢突然的变化。<br />
美国堪忧<br />
<br />
　　《新世纪》：你如何看待目前的全球经济复苏状况，今年会有经济二次探底的风险吗？<br />
<br />
　　索罗斯：复苏如预期般是疲弱的。但是现在越来越多的人担心负债率的上升，这在一定程度上抵消了刺激政策的效果，增加了二次探底的威胁，而对主权债务的忧虑，也加重了这一预期。<br />
<br />
　　《新世纪》：在后危机时代，美国应该如何吸引资本流入，以弥补经常项目的赤字？长期而言，美国应该如何解决财赤问题？<br />
<br />
　　索罗斯：目前，美国的经常性账户赤字正在减少，并且在可预见的将来将持续减少。这种变化是好现象，但同时也伴随着痛苦。如果美元兑人民币的汇率维持不变，要实现减少赤字的目标将是相当困难的。<br />
<br />
　　《新世纪》：美联储今年会加息吗？如果美国持续低息政策，中国及其他国家如何应对？<br />
<br />
　　索罗斯：我认为美联储今年不会加息，由于身负巨额财政赤字，美国政府似乎很难继续再维持财政刺激方案，刺激性的货币政策将持续一段时间。<br />
<br />
　　如果中国政府允许人民币升值，从而将通胀输出给美国，那么可能美联储会考虑加息，从中国的角度看，这对中国来说也是一种保持经济健康发展的举措。<br />
<br />
　　不过，美国政府还会持续量化宽松政策，这使得中国政府很难采取加息的办法，这也是中国政府为何选择提高银行法定准备金率，而不选择加息回收流动性的原因。<br />
<br />
中美新秩序<br />
<br />
　　《新世纪》：在经济危机之后，你认为全球主要经济体已经采取足够的措施以解决世界经济的基本面问题了吗，比如国际不平衡问题？<br />
<br />
　　索罗斯：没有。国际不平衡的问题正在持续扩大。在这样的背景下，中国巨大的经常性账户盈余就显得非常显眼，这就是为什么人民币升值的压力始终存在。目前，修正这种不平衡的进程尚未开始。<br />
<br />
　　我相信，全球经济的复苏与中国的持续繁荣，都将建立在更好的国际合作之上，尤其是中美之间的合作。然而，中美关系在最近却出现了反方向发展的趋势，我对此非常忧虑。<br />
<br />
　　《新世纪》：在你看来，目前全球对气候变化及绿色能源的关注，是否可以成为全球新的驱动力？这有内在驱动还是一场新的游戏？<br />
<br />
　　索罗斯：新能源无疑将成为世界经济新的增长点，但其前提是国际社会达成共识，必须为碳排放指标定价。如果国际社会没能对此形成价格上的共识，谁会来为相关领域的投资埋单呢？<br />
<br />
　　《新世纪》：你认为目前监管制度改革会否影响资本流入，这会否是一种隐晦的金融保护主义？在你看来，金融监管改革最重要是哪一部分的革新？<br />
<br />
　　索罗斯：我最近提出了一项关于使用特别提款权的提案，用以扶持新能源领域的投资。这项提案已经得到了IMF的支持。我希望中国也能对此施以援手。IMF总裁斯特劳斯-卡恩已经表示将在近期正式提出这一提案。<br />
<br />
　　如果国际社会不能对国际监管机制达成共识，现有的多边体制将岌岌可危，从而对全球经济造成不良影响。在我最近的一次演讲中，我已经提出了如何建立全球监管体制的指导性原则（这包括监管机构要防止泡沫变大、监督金融机构的产品甚至指令性放贷等一系列加强监管的措施――编者注）。<br />
<br />
　　《新世纪》：近年来越来越多的人开始谈论人民币国际化的问题，你如何看待中国政府对于人民币国际化的态度？如何做到国际化？<br />
<br />
　　索罗斯：中国当局对资本账户的放开，很可能是缓慢的。我乐见国际金融体系改革以达成新的国际协议，这其中也包括货币体系，一个新的布雷顿森林体系可能因此而诞生，人民币成为国际货币，这是我们期待看到的。<br />
<br />
　　进一步说，过去的全球化理论认为市场可以自我管理。这是错误的。特别是当英美放松监管时，其他国家也不得不如此操作，以防止资本流失到其他国家。<br />
<br />
　　过去的货币合作机制，尤其是IMF投票权的分配已经过时了。现在，我们面临的挑战是建立一个新的布雷顿森林体系，重塑全球金融系统。为了实现这个目标，中美两国的合作是不可或缺的。这两者将在新体系中发挥领导作用，如果中美不能达成共识，那么所谓新体系就是无稽之谈。<br />
<br />
　　本刊实习记者浦泓毅、陈琴对此文亦有贡献</div>

							
							
							
							
