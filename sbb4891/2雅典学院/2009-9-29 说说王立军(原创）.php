																											<div id="postmessage_263783" class="t_msgfont">　　　　　　　　　　　　　　　　　　说说王立军 <br />
<br />
&nbsp; &nbsp;　　文/东方安澜 <br />
<br />
&nbsp; &nbsp;王立军是重庆市的公安局局长，我是看了《南方周末》2009、8、20、A5版才知道的。记者黄秀丽发自重庆，标题是《扫黑风暴中的公安局长》。报道赞扬王立军以雷厉风行的铁腕铲除了一批重量级的黑社会集团。 <br />
<br />
&nbsp; &nbsp;柿子拣软的捏，话拣容易的先说。先说王立军的从严治警。报道分四个副标题，《要扫黑先治警》是最后一节。王立军在治警理念上提出“以业务支撑政治”“凡是不好好干的领导干部一律拿下”。在具体行动上落实“他要求分县局、处级以上的领导干部每周工作5天以上，每天工作14个小时。” <br />
<br />
&nbsp; &nbsp;报道中举了两个具体事例。一是某分局提拔干部不符合程序，后市局政治部发文件予以了纠正。二是王立军经常半夜给各派出所值班室打电话，如果没人接，第二天派出所所长立刻会被叫去“收拾”。<br />
<br />
&nbsp; &nbsp;王立军从严治警，报道说，警局内部对他看法分为两坨人，一坨人认为王立军作风霸道，他搞的那套累死人；一坨人认为他工作能力强，把民警的工作积极性都调动起来了。现在地球人都知道，手中有权的人，混个吃喝，弄个红利，只要不过份，这也不算什么，从报道上看，王立军这么一来，断了手下人的“第二福利”（我的发明，姑且这么称吧），报道中也提到“……以前警察朋友经常约出来耍的，现在都约不出来了。”这么一来，王立军必然遭手下人忌恨。<br />
 <br />
&nbsp; &nbsp;我分析，手下人对王立军的态度分三部分：一部分人对他崇敬，这是刚刚警校毕业从警时间不长血气方刚的年轻人；二部分人最多，持观望态度，纪律紧，他就谨慎，保住饭碗，有机会就捞点红利，如果清水衙门的时间太长，对王立军的反弹就越深；三部分在上层，屁股上有屎迹，但又有后台，王立军这个级别也动不了他，王立军石头猛，就冷眼旁观看王立军出洋相，甚至伙同某些势力帮王立军掘陷阱，等着他一昏头往下跳。毕竟他们是地头蛇吗。 <br />
<br />
&nbsp; &nbsp;从海瑞到林则徐乃至朱F基，有一个无奈的事实，不能把理想和现实脱节。不能以个人的品行要求所有人。断了别人的奶，就为自己头上竖了一把刀。长期如此，王立军必将陷于孤立，成“光杆司令”。“光杆司令”肯定一事无成，兵强马壮才能气势声威。谁上台都得有一批忠实的手下执行你的意志，把你的意图落实在行动中。 <br />
<br />
&nbsp; &nbsp;所以，我的结论，王立军如此，短期可行，推行强权，树立威望，收到治安实绩。 <br />
<br />
&nbsp; &nbsp;说说扫黑。黑是黑社会，黑恶势力。我实在搞不懂，近些年来，报纸电台电视拼命反黑，可就是黑势力越反越多，“一波还未平息，一波又来侵袭”。 <br />
<br />
&nbsp; &nbsp;那么，重庆的黑势力是怎么坐大坐强的呢？ <br />
<br />
&nbsp; &nbsp;现在重庆的一哥是薄熙来，可以肯定的是，这些黑老大在薄熙来调任重庆前就已经形成并坐大了，那么，作为薄熙来前任的贺国强干什么吃的？不知道自己治下有如此严重的黑势力？那不是失职失察吗？怎么还能进前九？ <br />
<br />
&nbsp; &nbsp;或者往英明的路上说，贺国强的一着棋，象乾隆一样为嘉庆留下个和|？我无从揣测。但无论如何，黑老大在贺国强任上坐大坐强，终究是贺的疮疤。<br />
 <br />
&nbsp; &nbsp;薄熙来狠命一反，贺必然要疼一下。疼多少我就不知道了。 <br />
<br />
&nbsp; &nbsp;薄熙来之前在辽宁做一把手，把同样是辽宁的王立军要到重庆，作为一员大将。警察在任何国家都是重要的国家机器，王立军去重庆，不单任公安局长，还兼重庆武警第一书记和第一政委，王立军投到薄的门下，成了薄的一把钢刀。<br />
 <br />
&nbsp; &nbsp;王立军调重庆一年，摸清了脉络，探出了水深，小李飞刀闪电出击，大有敲山震虎之势。这只虎就未必是小小的文强。 <br />
<br />
&nbsp; &nbsp;我的看法，薄这一着，是敲掉不听话的，为自己造势，也培植亲自己的社会中坚势力。至于反黑不反黑，大家心知肚明。不出意外，十八大2012年在即，薄要跻身前九，现在得抓住时机，加大各方面的造势力度。 <br />
<br />
&nbsp; &nbsp;报道中说王立军是扫黑英雄，嘿嘿，反黑不反黑，王立军不会不知道，头顶上帽子前沿的那粒东西是最大的黑，王立军甘当薄的马前卒，也有他自己的小九九。1958年出生的王，如果薄能进前九，那算跟对人了，有七八成把握能做一任公安部长。 <br />
<br />
&nbsp; &nbsp;如果在2012年薄寿终正寝，王立军失却了薄的撑腰，说不定成第二个文强。我不相信文强是好人，正像我不相信文强有当重量级黑帮的“黑保护伞”的资格。 <br />
