<br />
就是这货了，据说叫司南，相传是最早的指南针，教科书上上镜率挺高，CCAV的探索发现栏目还拿这货当标识。<br />
<br />
1这货不顶用，根本不能指南，中科院试制的也不行。<br />
<br />
2这货没有任何出土文物来证明其存在过，连个古书上的插图都没有。<br />
<br />
换句话说这货就是当朝为了激起屁民们的民族自豪感，以便让他们当大国撅起的燃料的时候可以心甘情愿一点而杜撰的。参考文献就12个字“司南之杓，投之於地，其柢指南”语出《论衡》<br />
<br />
有句话叫结构决定功能，指南针最早出现的时候，小磁针浮在水面上，一能减小摩擦，二能保持水平，后来加工技术改善了，旱指南针用点接触的结构来实现前述的两个目标。大勺子放到铜盘上摩擦力达不说，也不能保证水平，能指南才怪呢。<br />
<br />
<br />
&nbsp; &nbsp;<img src="http://lh3.ggpht.com/_WE_rz8Ihp0o/R018ODMF2mI/AAAAAAAAABg/KwWnqlBF2fU/s800/DSC00177.JPG" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
<br />
<br />
这货想必大家也认识，跟司南是一路货，没有出土文物，没有图片记载，就干巴巴一百多汉字，于是又一项中华民族的伟大发明诞生了，世界上最早的地震仪。可惜唐山大地震的时候这货就放在北京，球都没掉到蛤蟆嘴里。<br />
<br />
其实根据古籍做一些复原啊什么的并不是坏事儿，好使不好使无所谓，万一好使呢？这事儿老外也干，达芬奇的那些草稿都有人拿来蛋疼的试制。恶心就恶心在不好使还大张旗鼓的宣传，书上也写电视也播的，没羞没臊。意大利人从来不因为有达芬奇的手稿就声称直升机是意大利发明的，而且人家起码还有个图呢！<br />
<br />
我们从小脑袋里被强行灌屎，所以各种如唐博士那样不靠谱的鬼话都堂而皇之的写进教科书或者被官方大加宣传，比如说黄继光舍身堵抢眼这种严重违反物理学定律和人的生物学本质的传说，如此这般，说明黄继光同志根本不是人，明明是打入人族内部的虫族刺蛇嘛。还有周扒皮的半夜鸡叫，如此这般，周扒皮同志肯定是个精神病患者，作为一个资深地主，居然连日出而落日落而息的道理都不懂。而且他就不怕长工们消极怠工么？他们家鸡还得患有内分泌失调导致的生物钟严重紊乱。那这样一个家里有病鸡的精神病地主来做典型宣传，地主这个阶层是不是有点冤？<br />
<br />
中国人喜欢以自己的意识形态和官方的教导来反驳别人，而不是以客观事实和事物的内在规律，我华有个俗语叫“假大空”，这似乎不是本朝的专利，古已有之，动不动就子曰诗云俗话说的好，子曰的就对？俗话说的一定就好？ 这样做的好处就是自己不用思考了。别人给他一个结论，他不问问why。别人给他提出一个目标他也不想想how.<br />
<br />
于是世界唯一的逆向工业革命在我华上演了，超英赶美大炼钢铁，问题是，没有那么多铁矿和煤炭，不管，就是要超英赶美。现在情况比58年好点，没这么气吞山河的了，但是把字句还是不老少。比如说“把XX大学建设成世界一流的研究型大学！”“建设XX主义XX社会”&nbsp;&nbsp;“XX社会花似X，科学发展势如X”&nbsp;&nbsp;“公平正义比太阳还要光辉”“让中国人活的有尊严”………… 我就很纳闷啊，柏林洪堡大学是世界第一所现代大学，现代大学的标志之一就是学术自由，不依附于任何政治势力。某国一所现代大学都没有，连欧美19世纪的理念都不能实现，他怎么就能整出来世界一流的研究型大学呢？这是坑爹呢？<br />
<br />
与“让中国人活的有尊严”相比“风能进，雨能进，国王不能进！”就实在多了，字数不多，却把how to 使英国人民活的更有尊严这个问题表达的言简意赅。米国建国大业的时候口号也很朴实，根本没有什么“为了建设资本主义的美利坚合众国而努力奋斗！”连“无自由，毋宁死！”都没有，人家的口号是“ No taxation without representation!&quot; 无代表不纳税。多朴实的句子啊，缺乏激情但是充满了理性。<br />
<br />
中国主流意识形态的儒家喜欢强调道德的作用，但是中国历史上却周期性的出现礼崩乐坏，道德沦丧，儒家还喜欢描绘“王道乐土”,但是发生正在这片土地上的往往是生灵涂炭易子而食。这跟超英赶美超死几千万人，在挖掘机碾过的尸体上去建设和谐社会，在没有学术自由的前提下去建设世界一流研究型大学是一样的道理，不顾事物的客观规律的霸王硬上弓，而且往往还是南辕北辙。中国人就是喜欢先意淫到高潮，高潮过后是无限的落寞，凭空还多出几分本不该有悲怆来！<br />
<br />
