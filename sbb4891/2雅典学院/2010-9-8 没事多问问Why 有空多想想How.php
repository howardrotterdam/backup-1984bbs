																											<div id="postmessage_818560" class="t_msgfont">米国的林肯林大统领曾经说过这样的名言“你可以在某些时间里欺骗所有的人，也可以在所有的时间里欺骗某些人，但你决不能在所有的时间里欺骗所有的人．”技术上讲这话一点没错。不过如果有人能够做到在很长的一段时间内欺骗绝大多数人,其效果就跟在所有时间欺骗所有人差不多了。其实许多为世人所熟知的逸闻趣事都是不真实的，比如牛顿被苹果砸就是讹传，本文开头林肯大统领名言也是讹传，不过这样的名人佚事都市传奇之类讹传无损于民智，安到谁身上都行，增添点笑料和谈资罢了。但有些谎言，以讹传讹最后就造成了某些民族全民智障。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;我华的说谎氛围特别的好，当谎言成为常态，久而久之屁民们似乎失去了怀疑的精神，以至于唐博士能招摇撞骗这么久，最后揪出他的还是一个美国人。这样说似乎有事后诸葛亮之嫌，不过唐博士真的是一个很蹩脚的骗子。说自己是哪的不好？非说自己是CalTech的，像这种世界超超超一流大学的理工科PhD都是一些在某专项领域爬到世界之巅的人，一个CalTech CS的PhD不做学术不搞研发对自己的资源对社会的资源都是一种极大浪费，这样的人去做职业经理人，沃顿、哈佛、浮夸等等商学院情何以堪？当然不是说这样的人就不能转行，但是人的精力是有限的，唐博士这履历到这里就已经十分值得怀疑了，而且吧，更为关键的是加州理工规模十分十分有限，学生很少，屁大个学校，谁不认识谁啊？你没在那念过就不怕念过的揭穿你？换了我我就说是UC Berkley 的，起码学校大，不容易被揪。并不是说一听唐博士是加州理工的就能立刻看出他是假的来，起码凭我对美国学术系统的了解我心里会画个大大的问号。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;其实怀疑唐博士还是需要一定水平的，下面这个东东随便哪个理工科的大学生就能打一下假<br />
<br />
