43．哪朝哪代饿死了这么多人？毛泽东纪念堂你可以去，但你进去了别忘记了，那是一个靖国神社，里面供奉的是一个双手沾满人民鲜血的刽子手！靖国神社，可以参见，不能参拜，参拜了就是丧心病狂的法西斯极右分子。我开车到天安门广场，我身上起鸡皮疙瘩！四千多万冤魂锁定我，太可怕了！那个地方（毛主席纪念堂）应该改成“大屠杀纪念馆”，“ 殉难者纪念碑文”：死于1949到1978年政治恐怖者永垂不朽。几千万人活活饿死，你还说是伟大领袖？这个民族就是猪一样的民族。在德国你能说希特勒是伟大领袖吗？（面对摄像机，袁腾飞说：呆会把这段视频删了，有人说我说了我也不承认）---用前面或其他一些骗子对毛泽东的恶毒污蔑的谎言，再续更加恶毒的诅咒。诅咒后，又害怕遭报应，叮嘱摄像师把这段删除了！如果是大丈夫，如果是实事求是作宣传，又何于怕得如此？！正如鲁讯揭露的：古今中外的反动派是这样一种复合型生物，它们有如狮子般的凶心、狐狸般地狡猾、兔子一样的胆怯！　　<br />
<br />
44．毛泽东不是为了中国好，他是为了自己的权力好。他为什么要造原子弹，他不是为了保护中国，而是为了保护他屁股下面的那把椅子。如今天的北朝鲜，饿死几百万人，但他能造原子弹。原子弹还没有茶叶蛋管用，它好歹能裹腹。不造原子弹还可以少饿死点人。造原子弹是为了不遭受侵略，遭受侵略会造成南京大屠杀；结果因为你造原子弹饿死的人比南京大屠杀多的多。---把中华民族伟大的科学英雄为抗御帝国主义核讹诈、争分夺钞制造原子弹，胡说成为毛泽东个人保位子的行为！把中国制造原子弹的伟大功绩，说成是因制造了原子弹造成的饿死人数比南京大屠杀多的多的罪恶！这家伙对他人的违法恶毒不实污蔑和攻击，到了无所不用其极的地步。　　<br />
<br />
45．中国人认识到了毛泽东的倒行逆施，开始纠正了。62年召开七千人会议，初步总结了大跃进中的经验教训，为反右倾运动中被错批判的多数人评反。毛泽东假惺惺作了一个自我批评。在会上，刘少奇讲“错误与成绩”比例关系上与毛泽东唱反调。毛泽东就要废除刘少奇这个太子了。谁立林彪做新太子。林彪在会上说：历史的经验已经证明并将继续证明，什么时候我们沿着毛主席指示的路线前进，什么时候我们就战无不胜；什么时候我们背离了毛主席的路线，我们就要打败仗。毛泽东舒服，每一个汗毛孔都舒开了，这个人好！这为文化大革命打下了伏笔。要废长立幼。---把无产阶级通过文化思想教育革命的方式挽救走资派，说成是封建王朝要更换太子！这个袁某人污蔑人的水平怎么这么“不着调” 啊？比起他讲过的严嵩对岳飞的“莫须有”的污蔑术差多了！　　<br />
<br />
46．成绩部分，武钢、包钢两大基地，大庆胜利大港油田，石油全部自给；兰新、包兰铁路；原子弹、人工合成牛胰岛素。人工合成牛胰岛素是中国人唯一一次可能获得诺贝尔奖的机会。结果我们报了几百人，扫厕所的人也报了，人家当然不批了。瑞典国王能与扫厕所握手吗？这不是污辱我们国王吗？---铁一样的成绩摆在那儿不得不承认。但是，不经意的语言中，又暴露了他瞧不起劳动人民的剥削阶级思想与灵魂。　　<br />
<br />
47．对左倾错误只是调整，阶级斗争为纲没法纠正。发展到文革，这在中国5千年文明史上，甚至在1万8千年的人类发展史上，都是最黑暗的10年。能与之勉强媲美的只有斯大林与希特勒。---极力妖魔化毛泽东、恶毒污蔑无产阶级政党、其罪恶目标就是，全面否定公有公正为理想的社会主义制度、彻底推翻无产阶级专政，全面复辟人吃人、人剥削人的、万恶的私有剥削制度。　　<br />
<br />
48．毛泽东发动文革的理论基础。毛泽东认为：中央出了修正主义，党和国家面临着资产阶级复辟的危险。如果给毛泽东诊断一下，他属于幻想型精神分裂症。---实践是检验谎言的唯一标准！现在一些地方修正主义推进私有化、买办化、卖国化的滔天罪行，无可辩驳地证明毛泽东当年对走资本主义道路的当权派进行思想文化教育是何等英明！　　<br />
<br />
49．宋任穷的女儿叫宋彬彬，毛泽东说：革命不要彬彬有礼。于是宋彬彬，改为宋耀武，文革期间宋耀武亲手打死了7个人。文革后，跑到美国去了。听说归依佛门，忏悔。来世变成一个苍蝇、跳蚤啊，地狱轮回，她永远不能超生。文革的人为什么没有罪恶感？毛泽东是伟大领袖，毛泽东没有罪我怎么有罪？就象日本人一样，我为天皇打仗，天皇没有罪，我怎么有罪？---无产阶级对剥削阶级代理人的思想文化教育挽救革命，目的是为了他们保持本色、不要堕落为剥削阶级、不要脱离无产阶级革命队伍、其功德无量、光照千秋；只有反动没落的剥削阶级认为无产阶级革命是有罪的！袁氏还把无产阶级人民领袖比做帝国主义的天皇，把无产阶级革命者比做日本侵略者，何其恶毒而颠倒黑白！　　<br />
<br />
50．1967年二月，谭震林、陈毅、叶剑英等三位国务院副总理、四位元帅“三老四帅”挺身抗争，批评中央文革的做法。江青一伙污之为二月逆流。叶剑英拍桌子“你们把工厂农村搞乱了，你们还要把军队搞乱吗？”。谭震林声泪俱下：我一生三不该：一不该活这么大岁数，看到国家这么乱，我心痛；第二，我不该参加共产党；第三，当年我不该跟毛泽东去井冈山。江青马上给毛泽东打电话，毛泽东说叫谭震林接电话：谭震林你说话什么意思？你不上井冈山，井冈山是我请你去的吗？你不加入共产党，你可以退出吗。你不活这么大岁数，你不已经活了嘛，你现在也可以不活嘛。毛泽东作为一个领袖的素质摆在一边，作为一个人，后面加一个字，渣！他属于人渣。战友情、夫妻情、父子情，什么都没有，眼里只有一种东西，权力。---把无产阶级政党内部的批判与自我批判，污蔑成是权力斗争；对毛泽东进行恶毒的人身攻击；是可忍孰不可忍！无产阶级从来不否定，党内和党外的思想文化政治斗争、阶级斗争，有时会集中表现为权力的斗争。但是，无产阶级争的不是个人的权，是阶级的权、人民的权；而剥削阶级争的是个人升官发财的权、是反革命的权！各个领域的各类政治思想文化斗争，谁会取得最终的胜利？谁又会落得可耻的失败呢？历史已经证明并将继续证明：谁代表公有公正为目标的社会主义、谁代表非剥削阶级（即无剥削他人私有资产的无产阶级）的利益，谁就将取得最后的胜利！反之，谁背叛非剥削阶级即无产阶级的利益，不管他们是真老虎还是纸老虎，他们都最终可耻地、彻底地失败！当然，剥削阶级知识分子、走资派、帝国主义分子、四大窃贼们认识不到这个真理，他们只懂得追求私利、只知道剥削他人、只知道窃取公产剥夺劳动者、他们是人类公有公正竞争为目标的奥林匹克社会主义理想的死敌、是一伙有待人民思想、文化、教育和革命改造的、未进化的类人猿或类兽人。　　<br />
<br />
（对《近现代史_10_探索时期、十年文革（下）》的分析与批判待续）　　<br />
<br />
　 　<br />
<br />
二．极端反社污毛的袁腾飞为何被央视推崇？　　<br />
<br />
　 　<br />
<br />
令人深思和警觉是，一个如此露骨、极端、恶毒地反无产阶级政党、反社会主义制度、污蔑毛泽东的教育界的小丑，竟然被中国共产党直接领导的中央电视台反复地吹捧为“历史上最牛的历史老师”、并先后安排他上百家讲坛讲《两宋风云》、《塞北三朝之辽》，向中外兜售其类人猿世界观和非无产阶级反动思想。　　<br />
<br />
通过百度搜索我们还能得知，连党直接领导的新华网2008年07月16日 16:12:01也发表《中青在线专访：“史上最牛历史老师”的嬉笑怒骂(图)》一文。　　<br />
<br />
难道党领导的央视、新华网、中青社这些高端、专业的媒体不能识别鲜花和毒草？难道其中的少数人明知是毒草，偏要当成鲜花、并忽悠利用党和政府的媒体吹捧这个反毛、反社、污共、复辟私有剥削制度急先锋的袁腾飞？！　　<br />
<br />
这些现象警示人民：中国的社会主义教育、舆论和宣传阵地，正在被国内外、境内外、党内外的走资派、剥削阶级知识精蝇、四大窃贼代理人日益严重地渗透着；各级各类、各式各样、明的暗的袁腾飞们，正在通过各式各样的讲坛、媒体、教室、出版社、公权力，反复地、轰炸式地、向我们的下一代、向不明真相的中外群众，兜售越来越公开、胆大、极端、露骨、恶毒、疯狂地妖魔化无产阶级政党、攻击社会主义制度、污蔑无产阶级领袖谬论！　　<br />
<br />
面对国内外阶级敌人、右派右倾分子对四项基本原则的的疯狂进攻，党和人民应当高度警惕、及时揭露、坚决粉碎之！　　<br />
