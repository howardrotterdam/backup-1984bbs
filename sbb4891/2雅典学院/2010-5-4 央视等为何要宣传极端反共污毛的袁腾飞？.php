1．没有一个国家，公有制是搞得好的---希望袁腾飞睁开眼睛看看：毛泽东、共产党领导中国人民坚持公有制绝对为主体，用短短27年的时间，把一个积贫积弱、满目疮痍、惨遭帝国主义殖民侵略、被污称为东亚病夫的旧中国，一跃建设成世界第三大强国！八国联军再也不敢侵略我们！中华大地基本消灭了人剥削人、人吃人的类人猿社会制度。在私有化剥削化风起云涌的近三十年，华西村、刘庄村、南街村、海尔集团等万余个公有经济体内的人民，坚持公有制、拒绝分田单干和私有化改制，不仅成功防止了人剥削人罪恶制度的复辟，还不同程度地实现了共同富裕！这一系列铁的事实，如铁掌一样打在你的脸上，你不觉得疼吗？　　<br />
