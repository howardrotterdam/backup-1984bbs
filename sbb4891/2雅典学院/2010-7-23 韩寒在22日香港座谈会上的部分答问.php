问：看过你接受BBC的采访，是如何做到不踩钢线的呢？<br />
答：我想他可能想表达的是不踩红线和走钢丝，然后就把这个一起表达成了“走钢线”。事实上是这样，就我特别讨厌有人在写东西的时候说“每个人心中都有一个……”，我觉得这句话特别扯蛋。但事实上的确是对于每个写作者来说每个人心中都有一根钢线。可能我的钢线相对来说比较的宽泛一些，但是事实上，所有写东西的人我相信大家都有着几乎一样的追求，包括所有的媒体人。因为我经常觉得在一个国家里宪法应该是它最后的底线，但事实上宪法有的时候它会变成“沦陷”的“陷”，所以在那个时候，我相信媒体是唯一的底线。其实很多的媒体，有的时候在大陆大家可能觉得说这一方面的媒体一定是很进步的，那样的媒体它一定是非常的落后的，事实上不是。事实上年轻人都是差不多的，而且我相信任何的媒体从业人员都是有他的新闻理想、媒体追求的。我相信大家都一样，而且未来这根钢线一定会越来越远。反正最终我相信会没有这根钢线的，因为所有的线所有的东西都有它可以承载的分量，如果它太重了，它一定会崩溃的。<br />
 <br />
问：韩寒你好，看到你，我和我的闺蜜心里都小鹿乱撞。本来我就喜欢帅哥，何况是才子。我们特别欣赏你的文字和生活。你对世界杯有不少评论，请问本届让你记忆最深的球队和球员是？<br />
答：我对世界杯其实没有什么评论。当时看了几场朝鲜队的比赛，因为第一场看朝鲜队对巴西，当时就觉得朝鲜踢得还算不错，虽然没有什么章法，但还是挺拼命的嘛，可能是怕回国以后被枪决所以就特别的拼命。后来看第二场，看朝鲜队对葡萄牙，因为葡萄牙之前表现得不好嘛，所以很多朋友说葡萄牙有可能会被朝鲜逼平，甚至都取胜，但我总觉得不大可能，我觉得朝鲜还是可能会输，因为很多时候它事实上不是靠精神来获胜的，还是要靠技术，而且尤其是这种精神建立在给金正日将军开心开心的精神上，我还是觉得它不大靠谱。后来朝鲜的确是输了，当时我给朋友发了一个短信说，类似制度的这种国家啊，一旦硬起来还是能够挺硬的，但一旦崩溃起来那是一泄千里。后来我看了荷兰对西班牙的比赛，大多数比赛我都看了。我喜欢南美球队，我也喜欢阿根廷，但是阿根廷很早就出局了，没有办法。<br />
