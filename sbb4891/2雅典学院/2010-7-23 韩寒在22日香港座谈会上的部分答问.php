问：你是怎样回应你是韩国人的？<br />
答：身为一个韩国作家（笑），很高兴能和大家见面。但事实上我之前写过一篇文章关于这些东西。因为我们对韩国，事实上是有一些莫名的仇恨的。先说一下日本，当年在我出《独唱团》的时候，有一张小的画，那张画可能大家都挺喜欢的，所以我放在《独唱团》的封底，但现在没有了，现在被我去掉了，就是有一个没穿衣服的人拿着枪的那个。后来那张画放大了以后还做成海报，当时书都已经印好了，结果我突然拿回来发现那个画的手臂上刻了两个字叫“爱日”。当时这是一个连续漫画，后来这个连续的漫画被撤掉了以后这两个字放在这里就非常的突兀。当时其实想要表达的意思是那个人非常喜欢乱搞，但是单独作为爱日就很容易被很多民族主义者拿来攻击，对我来说无所谓，但我不想我的读者会被这些莫名其妙的情绪所骚扰，所以我收回了整个《独唱团》，销毁了封面和十几万册书，销毁了五十万个封面，所以这就是为什么导致它本身是七月一号上市的，变成了七月六号。并不是因为回避这个生日，就是因为这个问题。因为对于我本人来说，虽然我个人非常欣赏日本，在亚洲无论是从美术或者是从工业、礼仪等等很多东西都值得我们学习，但事实上我又讨厌他们，因为他们打断了我们国家的本来非常有希望的现代化的进程，否则的话就没苏联的什么事儿了。所以也挺遗憾的。但对于韩国人，其实我觉得韩国也根本没有那么不好，很多东西都是我们自己想象出来的，我们先在人家身上泼一点粪，然后就走过去说“哎你怎么这么臭”。包括说我是韩国人的那个帖子，其实也是我们自己人，可能作为开玩笑或者是恶搞的形式来发表的，其实也不是韩国人那么写的。所以我觉得无论韩国还是日本、中国，大家其实都可以在一起把酒言欢，事实上没有那么多的仇恨的，真的。<br />
 <br />
