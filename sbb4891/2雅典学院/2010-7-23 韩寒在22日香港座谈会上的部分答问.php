问：你最喜欢内地和香港的歌手是谁？<br />
答：说到这里想起刚才也在媒体会上说过的一个事情。香港我很喜欢张国荣，也很喜欢张国荣黄耀明那一批的歌手，台湾呢我很喜欢更多的是词人或者制作人，李宗盛罗大佑那一批。因为那个时候对歌词更加的看重，可能是出于我的职业吧，对歌词的关注要更多一点。讲一个刚才讲过的一个故事，就是我在机场的时候我很少看到那种名人会想要上去跟他说话啊或者怎么，但有一次我从上海坐飞机到北京，我就看到了李宗盛。当时李宗盛穿了一条特别花的衬衫，当然也符合他的这个性格，很花，一颠一颠就进洗手间了，我也不好意思跟着他一颠一颠的颠进去，我就在那里想，我顺便要等行李嘛，就顺便等李宗盛出来，结果这一等就是二十分钟，他都没有出来，一直没有出来过，我实在等不到他我就走了。但我还是很喜欢那一批词人的。其实周杰伦也很不错，他的歌的整体质量还算不错，他的几首歌我都挺喜欢的，但我对他写歌唱歌之外的一些审美不是特别的认同。<br />
