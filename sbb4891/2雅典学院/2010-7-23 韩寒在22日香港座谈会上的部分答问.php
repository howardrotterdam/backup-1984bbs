问：请问你的字那么漂亮，有没有想过出一本里面全是手稿的书，又让同学喜欢，又可以让更多人临摹一手好字？<br />
答：我其实当时想过出一本书全都是手稿的，但是事实上我觉得没有什么特别大的意义，因为我相信可能一个作者还是要靠他写的东西来吸引读者，而不是他写作之外的一些东西。有的时候如果我出一个纯手稿可能可以拿去稍微弄一弄做一个艺术品挣点钱，因为有的时候我也很羡慕那些艺术家嘛，出门骑个自行车“R”被车撞了，当然假设人没事，然后自行车被撞变形了，他就把这个自行车拿出来起了个名字叫“中国”，就在那卖一千万。所以我很羡慕很多艺术家，但始终我还是想做好我的本行工作，现在还没有想做自己（的艺术）。事实上我的字也就是说在中国的传统作家当中写得算稍微好一点而已，但是在真正的写书法的人当中其实根本就不算什么，主要是因为大家的字都太难看了。<br />
