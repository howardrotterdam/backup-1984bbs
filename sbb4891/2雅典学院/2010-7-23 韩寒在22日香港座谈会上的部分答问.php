问：和你同时出现的人中很多都移民了，你会离开中国吗？<br />
答：我不会。可能这个答案很多人不大愿意听到，如果我说“我会”就好了。我还真是不会，因为我喜欢的人都在中国，我不喜欢洋妞。我觉得我会一直留在中国，如果她欢迎我我会很开心，如果她，我相信她不会驱逐我，事实上无论如何，它始终是我的故土。这种感受很奇怪，去国外比赛的时候，虽然你可以感觉得到它们的确是很好，无论是社会制度还是人和人之间的相处，它们的确是无论科技民主各方面都在一个非常高的水平上面。但更多的你只是希望说如果将来我们的国家也是这样那该多好，而不是说想要去住到别人的国家里面。始终，很怪，包括他们对你的相处。他们当然很客气，但是更多的那种客气事实上是出于他们本身的一种礼教。我还是更希望别的国家的人想来移民我们国家，但我现在不建议他们现阶段来。<br />
