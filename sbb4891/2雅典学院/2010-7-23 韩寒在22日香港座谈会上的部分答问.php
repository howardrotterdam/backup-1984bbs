													<h2>韩寒7月22日香港书展读者见面会文字版</h2>
						
						
																											<div id="postmessage_755268" class="t_msgfont">韩寒7月22日香港书展读者见面会文字版<br />
 搜索了下，这个更全点。就发上来了。<br />
若不适合请版主处理。<br />
<br />
<br />
文字版根据现场录音整理 <br />
开场：<br />
韩：这是一个什么都可以说的地方，所以我就没什么可说的。把（见面会）大部分的时间都留给大家提问，有什么问题都可以来问我。谢谢大家今天来捧场。<br />
 <br />
主持人：今天既然是2010年香港书展里面的一个活动，所以我想我们还是要回归文学。因为书展上很多的读者都是冲着韩寒的文字来的，今年的书展他也有出版小说《他的国》的繁体版本，还有一本牛津出版社出版的他博客的杂文集《漂移中国》。他还有一本书《独唱团》，相信在场很多人都看过，现在发行量据说已经超过了八十万册，首印非常成功。韩寒，不如我们就从《独唱团》开始聊起吧。很多读者看过《独唱团》后觉得里面的文章都很有个性，你是用个性来解释“独唱团”这个名字的吗？可以给我们解释一下“独唱团”的意思吗？<br />
韩：不是。其实“独唱团”这个名字当时最早我是想留给我小说的名字，就像我爸爸想把韩寒这个名字，当时他想用来做他的笔名的，留给了我一样。这是我预备的一个名字。当时最早的时候我想把这本杂志叫《文艺复兴》，后来就觉得不好，因为觉得这个牛逼吹得有点太大了，另外一个原因是当局也没有批，当局觉得可能这个名字不好，所有带文艺的名字都不行，因为他们本身自己也觉得自己没有文艺，所以就没能用这个名字。后来就叫《独唱团》了。<br />
主持：你宣布要办这个杂志到现在，它中间有几个月的时间，我想知道的是这几个月当中你觉得最难的是什么，导致花了这么长的时间？<br />
韩：一个是金钱的压力，因为杂志社审稿的时候是需要成本的，一个月需要十多万的人员工资。当时坚持了大概一年多，付了两三百万的钱。因为我自己虽然是个畅销书作家，但是我平时也不大留什么钱，所以当时就比较穷，做鸭子的心都有了。但是一想觉得这个（做鸭子）跟别的不一样，因为听说这个不像做小姐可以一晚上就能（赚到），所以就想到要靠另外一种卖身的方式，就是做一些商业的活动。后来做了一些商业的活动把杂志社的开销什么的都给填平了。还有一个困难就是出版的时候，因为大家都知道有很多的审查，但这种审查其实并不是官方的审查，大家都觉得是新闻出版署的查你，中宣部的审查你，事实上不是的，是出版社的审查，还轮不到那些地方来审查你，那些地方往往不会审查你，交给出版社就行了，借刀杀人就可以，但是出版社审查得比较严。刚才我跟媒体朋友在一起做一个交流的时候也讲到过一个小故事，就是一个关于羊肉的故事。就是有一篇文章里面说这个人点菜点了一份羊肉，结果出版社说这个不行，这不能过。我问为什么，他们说因为点羊肉代表这个人没有点牛肉没有点猪肉，没有点猪肉就说明他可能不吃猪肉，那就说明他可能是个回民，这就牵涉到了民族宗教问题。然后还有比如说千禧年这种词都不能够放到文章里面，因为千禧年是一个宗教词汇。所以有一些各种各样的审查问题。他们并不是那么为难我，只是因为他们很谨慎，非常的谨慎，而我又想尽量多地保全各个作者的文章，所以换了很多的出版社，有些出版社说这几篇文章不行，说你换个别的出版社审审吧，别的出版社说这三篇文章一点问题都没有，（主持人：你总共换了多少家出版社？）换了四五家吧，但是这个出版社却说是那三篇文章有问题，所以每一个出版社都有不同的想法，花了很多的时间，后来也是做了不少的妥协。因为事实上我还是单纯的只是想做一本文艺杂志，因为如果你特别大篇幅的看着都是那些战斗性的文章会很累，而且网络始终是那些文章最好的一个载体。<br />
主持人：所以我们现在看到的这本《独唱团》里面，有很多的作家的大作是尽量的保存下来了。我想问你选择这些文章的标准是什么？<br />
韩：有趣。但不光光是有趣，可是有一个朋友说得很对，他在批评这本杂志的时候说，你可能太过于关注那些，比如说在尺度上，跟审查有关系的文章了，那只是所有文艺形式的20%，还有80%是无关审查的，但是你不能因为把注意力都放在那里，降低了那80%的文艺文学的标准。我觉得他说得很对，所以以后我们会更加的注意些。<br />
 <br />
主持人：说到这里我们已经有两个小条子递上来了。<br />
（韩寒开始自己读问题然后解答）<br />
问：作为年轻人的代表，你对你的同龄人是否有什么建议？<br />
答：没有什么建议。（笑）因为每个人的情况事实上都不一样，我觉得还是自己来吧。<br />
 <br />
问：你跟今天中国比较优秀的知识分子有过接触，比如陈丹青、梁文道等，他们对你的观念是否产生过影响？如果有，是什么？<br />
答：其实我很小的时候就看挺多书，他们的书我也看过不少。陈丹青跟梁文道都是现在写得相对来说比较好的作家，我个人非常的喜欢他们。<br />
 <br />
问：你如何处理恐惧？<br />
答：我就“啊”一声（大叫出来）。就这样处理恐惧。<br />
 <br />
问：现在你出书，赛车，看似很成功，会不会出现后劲不足的问题？比如说你的学历，你没读过大学，你的知识储备少了。<br />
答：这个问题问得很好，自从出了唐骏的事情以后大家都特别的关心学历这个问题。其实这个问题刚才对媒体也回答过，大家都很关心学历嘛，听了唐骏的事情以后很多名人都纷纷的修改了自己的学历。然后我也顺应这个顺势，也修改了自己的学历。因为以前在自己的学历那一栏上面我填的是高中，后来我发现其实我高中文凭没拿到，我应该是初中（水平）。所以我就把学历修改成初中了。谢谢唐骏。<br />
 <br />
问：《独唱团》里面有你写的一篇叫《我想和这个世界谈一谈》的文章，听说这是你的下一部长篇小说，现在是在《独唱团》上做连载。您这个标题让大家很感兴趣，“想和这个世界谈一谈”主要是指的谈什么？<br />
答：这原来又有一个书名。原来我想叫的书名叫《1988》，后来他妈的村上春树出了本《1Q84》，这（《1988》）其实是我想了好多天的一个书名又不能用了。当然我很喜欢村上春树，我觉得他写得也相当不错。一个是他有着相当好的情怀和非常进步的心态；另外一方面是日本作家写东西都特别嗦，他是相对来说不那么嗦的一个。（所以这个名字）到最后就改成《我想和这个世界谈一谈》了。<br />
 <br />
问：你和你的亲人会不会对你的处境甚至人身的安全有所担心？<br />
答：大陆其实没有大家想象得那么恐怖，所以虽然它很多时候也挺恐怖的，但是因为这些年事实上对文人对作者更多的采取的是说，大不了你不要说这个，或者说你说了以后就把它删掉，顶多是这样。但是对于人身的威胁或者伤害其实这些年都还算好。我担心吗？是的，我担心的，因为我担心回不了中国。因为在香港我还不认识谁，留在香港可能需要花很长的时间来适应。但事实上他们更担心的是我在比赛的时候的安全。为了打消他的顾虑，我带我爸爸坐过一次赛车，我带他去其实是想告诉他赛车其实是非常安全的一件事情，结果那次我还撞了。不过总体来说很安全，他们对我也很放心。<br />
 <br />
问：最近一个月博客都没有更新，在忙什么？<br />
答：其实我想更新过几次，但后来又都没有更新整整快一个月了。可能最近没有什么特别想说的吧。<br />
 <br />
问：《独唱团》以双月刊的形式发行，以后是否会保持这样的周期呢？你对《独唱团》阉割掉了多少最初的内容？<br />
答：这不是我阉割的。它现在是以双月刊的方式发行，我们甚至不能叫它是刊，因为它事实上还是用书号做一本丛书在出版，但是会转成刊号出版的。<br />
 <br />
问：今天看到你一直带着笑脸，请问你怎样保持这么好的心态？<br />
答：其实昨天我跟我女朋友吵架了，你们都没有看到我内心的酸楚。<br />
 <br />
问：在香港最想见哪位明星？<br />
答：男明星女明星有要求吗？在香港我想见张柏芝。其实我个人还是挺欣赏她的，真的。<br />
 <br />
主持人：我现在已经收到很多的小条。我今天是没有审查的，我会全部都递给韩寒，但是我会把它分分类。现在发现纸条里面有三分之一的问题在问你关于结婚，做爸爸的事情，我觉得你必须回应一下。<br />
韩：哎，真好。你不负责审查只负责分类，如果大陆的宣传部门都像你这样就好了。<br />
（继续自己读问题然后解答）<br />
问：你准备什么时候做父亲？<br />
答：其实这是一场机缘巧合，这些都没有办法来预判的。我有很多朋友想做父亲但却没有做成，有很多朋友只是随心一炮就成了父亲。所以这些都不能够去判断。<br />
 <br />
问：你这次来住在香港的哪个酒店？<br />
答：其实这个酒店离这里很近，我具体不清楚它的名字，因为昨天到得太晚了。就在那个会展中心后面，房号是...因为我一会就走了，所以可以告诉大家。<br />
 <br />
问：网上有一篇冒充你写你女友的文章，可以藉此机会真实的来说一下吗？<br />
答：对，那篇文章是冒充的。我一般不大会写我女朋友的，除非等我老的时候写回忆录的时候会写，而且那个时候可以把更多的事写出来，我相信他们也都能原谅我。<br />
 <br />
问：你对香港这个地方有什么看法？<br />
答：其实对香港这个地方，我个人是挺喜欢的，而且很早的时候就在大陆的电视上接触到香港。香港很多艺人到大陆回答问题的时候永远就是“呃，关于这一个问题”（模仿香港艺人发得不标准的普通话）。很多人说香港是一个没有文化的城市，可能是觉得香港人不大喜欢读书，或者说读很多的八卦。事实上所有的一切都是文化，八卦也有八卦的文化，电影有电影的文化。一个出过那么多好电影的城市，一定不是一个没有文化的城市，而且它对文化那么的宽容。事实上我感谢这座城市，它保护和庇护了很多华人，尤其来自大陆的文化人或者作家。我要谢谢这座城市。<br />
 <br />
问：今天你来香港说了很多的口头语，比如说NB，TMD，还有鸭子、小姐等，这些词汇给人感觉有些肤浅，不成体系，是因为来香港了吗？某报记者问。<br />
答：你是光明日报的吗？<br />
 <br />
问：你好像蛮留心香港的流行文化。香港的流行文化对你有影响吗？<br />
答：有。包括当时最早的时候林夕写的词，或者说张国荣的歌，陈百强，黄耀明，达明一派，很多很多香港的艺人，四大天王，还有台湾的。因为我一直经常搞混台湾跟香港的流行文化，但本身也有很多相近的地方，当然也有影响。<br />
 <br />
主持人：我打断一下，有一些问题是这样子的，涉及了个人隐私资料，我觉得不大方便念，包括她的姓名、电话号码都写出来了，“你风趣幽默，长得可爱，正好是我喜欢的类型，我们可以约会吗，我的电话是……”。<br />
韩：（接着念）还有最后一句，“很高兴听你说你和你女朋友吵架了，请快快致电我，我长得很像柏芝。小桃。（署名）”这是哪一位朋友？敢写的就要敢站出来。<br />
