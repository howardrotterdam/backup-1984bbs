																											<div id="postmessage_242812" class="t_msgfont">北京, 庆六十大寿，隆重阅兵，为振国威、军威，为民族自信、自豪，为威慑敌对势力，所以，即使在全球金融危机情况下，也不惜重金，轰轰烈烈举行。为保证风光体面，而又顺顺利利，从三月起，用五个多月时间，用最新的建筑材料，大修长安街。用330万株地植花卉，设22个花坛，精心打造长安街。为海陆空三军实地演习，为学生实地彩排，从九月六日起，又实行交通管制。北京，你不计代价，煞费苦心，投大量精力，庆生日，搞阅兵，我实在不敢恭维你。 <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;一个国家的威望、威慑力，一个军队的影响力、战斗力，一个民族的自信心、自豪感，从来不是靠铺一段路，摆几朵花，放几门礼炮，叫几声口号就有了。世界人民的眼睛是雪亮的。只有你的老百姓的生活水平提高了，丰衣足食了，丰富多彩了，环境美好了，言论自由了，生命值钱了，人家才羡慕你，向往你，害怕你。从来不是靠什么阅兵来提高、展示。君不见，人们纷纷申请移民新加坡、美国、日本、加拿大等国家，不是因为他们搞阅兵、有核武。要说阅兵、核武，我们和我们的邻居：朝鲜、巴基斯坦、印度样样都有，一样不缺，但这些国家的子民们，却纷纷以留学、旅游、投资、投亲、偷渡等方式，不惜代价，不择手段，一批批往“外走”，争当外国人，可见，庆生日，搞阅兵，是不能解决问题。相反，多此一举，属于形象工程，劳民伤财，折腾百姓，折腾军队。 <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;阅兵是一种什么玩意呢？具体来说就是一批穿着军装的人，在一些武器的陪衬下，为首长举行的列队表演。简单说就是格式化的军队戏剧。靠军队演戏来振国威、树信心、吓敌人，这简直是世纪笑话，是一种自欺或自慰的无能表现。 <br />
<br />
&nbsp; &nbsp; 是英雄或狗熊，不是自吹自擂，是e是马，拉出去溜溜。我们的军队是不是威武雄壮，是否有战斗力，是否有威慑力，不是靠彩排出来，我们的军队是用来保护国家领土主权完整，保护人民生命财产安全，不是用来庆生日，搞演戏。如果是要演戏，办剧团或请马戏团不是更好吗？何必养军队呢？因为，当我们的主权遭受侵犯时，急需军队；当我们的同胞生命财产受到威胁时，急需军队，而不是需要马戏团。为此建设一支强有力军队是必要的。但花巨资为首长搞表演、庆生日、搞形象工程，不是建设军队，而是削弱军队，这样“耀武”绝不能“扬威”，更不能提高民族自信心、自豪感。 <br />
<br />
&nbsp; &nbsp; 大家知道，钓鱼岛自古以来就是中国领土，可现在是日本人的控制之下。中国的领土中国人不能到上面去。中国人去了，伤的伤，死的死，被抓的被抓，难道这就是中国的威武吗？这就是中国的威慑力吗？南海也是中国的南海，共有28个岛礁，却被周围小国占去20个；我们的渔民到那里捕鱼，被一群小国的军舰把船撞翻，把网割破，把人抓走。这时，他们多么希望看到我们威武的解放军呀！多么希望中国的名字能吓走他们呀！可惜，国人像猪一样被抓往他国，叫天不应，叫地不灵；藏南9万多平方公里的中国领土，也被印度占领；我们的威慑力哪里去了？我们已隆重举行了13个阅兵仪式，难道是第14个阅兵才能吓走他们吗？还有印尼98年，5.13惨案，华人被抢、被奸、被杀；再有现在的缅甸，又炮击华人为主的果敢特区，难道他们都不知道北京有阅兵吗？有核武吗？ <br />
<br />
&nbsp; &nbsp; 北京，领海领土被占，同胞受辱，总是听到你说：不干涉别国内政；或搁置争议。或不得已才叫几声“抗议”。北京，你如此作为让人心疼，难道这就是你所说的“韬光养晦”吗？难道你真的能相信在天安门“演戏”，就能镇得住如狼似虎的“番邦”吗？难道你真的就是一头纸糊的老虎？没有脾气，不咬人吗？或真是一头“庞然大物”，但“技止此耳”的黔之驴？北京，我实在不懂你的心。 <br />
<br />
&nbsp; &nbsp;“养兵千日，用兵一时”，国民有难，正是用兵的时候，只有立即出动军队，才有威慑力。别人派出去的是军舰，我们派出去的是渔政船，没见军队的影子，哪有什么威慑力？就算军队去也未必能有威慑力，只有打胜仗才有威慑力。就像网友所说：实战才是最好的阅兵。但我不是主张战争，我也知道宽容与和平的可爱，我也追求宽容与和平。但有世界就会有矛盾，有矛盾就会有冲突。当别人欺人太甚，忍不可忍的时候，当别人置我于死地的时候，叫我怎能不出手？ <br />
<br />
&nbsp; &nbsp; 1911年5月13日，墨西哥300多华人惨遭杀害，清政府马上聘请国际调查员调查，向墨政府提出强烈抗议，并且立即电令正在北大西洋游弋的重巡洋舰“海圻”号，前往事故地保护华侨，威胁开战。墨西哥政府在外交、军事的压力下被迫同意：惩办凶手、赔偿损失、安慰家属，并设法保护华侨。清政府腐败无能，但在这件事上做得非常漂亮，大快人心。就像美国政府，为解救被朝鲜扣留的两名美国女记者，派出前总统克林顿出面谈判，并用专机把人接回美国。这才叫以人为本，这才让人民有自信心、自豪感；不像我们的渔民被那些小国抓去，亲属倾家荡产也赎不回人，这样的国民能自豪吗？ <br />
<br />
