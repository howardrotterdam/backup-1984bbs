&nbsp; &nbsp;对中共来说，今年是多重危机集中暴发的一年，当前中共各地官僚强拆征地变本加厉、民怨沸腾、通胀继续飙升、股市暴跌、楼市、金融岌岌可危、外资撤逃、工业萧条、失业加剧...经济大崩溃即将到来，中共政权岌岌可危；更兼胡锦涛在达富尔、西藏问题上处理失当、国际抵制奥运声势高涨，其本人威信大挫，在党内外非议如潮，诚可谓内外交困；在台海问题上，台独势力败选下台，少了一个容易借之以转移视线的敲打靶子，而奉行民主“终极统一”原则的马英九即将上台，将对中共带来更大挑战，而台湾此次顺利地换届大选，更拆穿了中共的“国情论”谎言，凸显了中共政权的非法性...... <br />
