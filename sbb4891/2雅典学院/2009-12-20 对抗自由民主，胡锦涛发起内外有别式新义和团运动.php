&nbsp; &nbsp;不寻常的是，在高效压制国内的愤青运动的同时，中共胡中央继续开动国内外御用、亲共媒体，大肆妖魔化西方国家媒体、政府，诬蔑达赖喇嘛，继续煽动反西方民族主义狂热、煽动对藏人的仇恨。 <br />
&nbsp; &nbsp;同时，中共利用西方国家的自由民主制度，通过驻西方国家的领事馆，拨发大笔经费，“上不封顶”，大量雇佣临时工作人员，租用大批旅游车辆，以共特和权贵既得利益集团在海外子女为核心，加紧收买笼络大批愤青留学生和海外华人愚民，竭力发动大规模的示威游行，声援北京奥运会、反对“藏独”、反对西方国家媒体、政府对中共暴政的批评，中共甚至指挥亲共游行团体，暴力干涉反共异议团体、殴打和平抗议的抵制奥运人士，完全无视他国法律。目前，亲共分子暴力干涉反共异议团体、殴打和平抗议的人士已经在美国发生；反对西方媒体的大规模亲共游行示威也正在德国、法国、美国上演...... <br />
