																											<div id="postmessage_805230" class="t_msgfont">来源：作者来稿 作者：伊利夏提 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;两年前，我参加杨建利博士在波士顿哈佛大学主办的族群青年领袖研习营对话会议。最后一天，我们维吾尔人组成的三人组介绍维吾尔人的情况，并回答族群对话参与者有关维吾尔人的提问。我记得，最后一个提问者是一位来自纽约的中国民主党党员，他直接点名向我提了两个问题。 (博讯 boxun.com)<br />
<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;他首先问我：“你们能代表全体维吾尔人吗？”<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;他的第二个问题是：“世界维吾尔人大会的章程上，你们保证要用非暴力的方式去争取维吾尔人的一切权利，在非暴力这点上，你们能坚持到底吗？”<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;对第一个问题，我坚定地回答：“是的，我们能代表全体维吾尔人！因为任何一个有尊严的、热爱自由的维吾尔人都不会愿意当他人的奴隶，生活在异族的统治下。维吾尔人热爱自由、平等，热爱自己的祖国――东土耳其斯坦，热爱自己的文化，热爱并忠诚于自己的信仰――伊斯兰教。所以没有任何一个维吾尔人愿意生活在中共政权统治下！”（大意）<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;对第二个问题，我的回答激起了很大的骚动。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;我非常自信地回答说：“我们会坚持和平的斗争方式，坚持非暴力。但是，如果中共不改变其在东土耳其斯坦的恐怖统治，如果一再挑战我们的承受底线，我们很难保证维吾尔人不会以暴力回答中共的残暴！”<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;我接着说：“为了我们的祖国――东土耳其斯坦的自由、独立，为了维吾尔人民的自由、平等，为了坚持我们的信仰、文化，维吾尔人也不放弃在最后时刻使用任何手段去争取我们祖国的独立、自由，东土耳其斯坦人民的平等、解放！”<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;我最后强调说：“在力量对比上，我们很弱，中共很强大，但我们不怕。我们有信仰，如果维吾尔人被逼到了绝望的死地，我们可以让东土耳其斯坦变成另一个巴勒斯坦，另一个车臣！如果有人认为为了自己民族的自由，为了自己祖国的独立而奋斗的人是‘恐怖分子’的话，那我就是‘恐怖分子’我不怕这个帽子”（大意）<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;当时会场立即陷入了骚动，主持人也无法控制场面了。一位欧洲来的女士很不高兴得问我：“你怎么能提暴力呢，我们是非暴力的，要坚持非暴力。”<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;我问她：“如果有人要霸占你的家、要强奸你的母亲，你是要和她非暴力讲道理呢，还是要和她搏斗，去拯救你的母亲？！”<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;当然没有人回答我的问题，他们只是觉得我不应该这么说。但我说的是实话，我说出的是大多数维吾尔人想说的话，也是举世公认的世界公理！<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;欧洲哪一个国家的独立没有过流血、牺牲？意大利的独立、二战中法国抵抗运动，美国的独立战争，哪一个不血腥！？看看好莱坞大片《爱国者》的血腥场面。维吾尔人的反抗运动和这些国家的独立运动比，在血腥程度上真的是小巫见大巫。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;至于巴勒斯坦人问题，世界一直是支持他们的正义斗争的，（包括中共政权在内的支持，中共为其私利，一直支持巴勒斯坦解放组织。就像几年前支持阿富汗塔利班政权，支持波尔布特、英萨利柬埔寨恐怖政权一样）。巴勒斯坦人为了他们的正当权利进行了各种形式的斗争，这包括武装斗争。到最后主流派放弃了武装斗争，但并没有得到国际社会所承诺的在完整的巴勒斯坦土地上建国的目的，巴勒斯坦人当中的一部分绝望者采取了极端的反抗方式。我不同意他们的残杀无辜，但我可以理解他们在绝望中的挣扎。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;车臣也是同样的问题，俄国在十八世纪末占领了车臣人的土地。在斯大林统治时期更是将车臣人以不可信任为由，整体发配到西伯利亚、中亚等地。几乎一半的车臣人死在被迫的徒步迁移中。现在他们要求自己的独立、自由，合情合理。是车臣人的正当权利！当敌人以残暴回答自由的呼声时，被压迫者有权选择自己的反抗方式！<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;维吾尔人的自由呼声，中共听到了，世界也听到了。中共以强盗逻辑使用最野蛮的方式来镇压维吾尔人的反抗，文明世界以经济利益为借口采取绥靖政策，纵容中共的恐怖野蛮统治！<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;当正义得不到弘扬时，当弱者的呼声被强权所淹没时；弱者会走向绝望，绝望中的人必然是极端的！<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;维吾尔人在孤独的绝望中，作为弱者，在走向极端。这不是维吾尔人的选择，是中共的暴政所迫，是文明世界对中共暴政的沉默、纵容所逼。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;为了避免暴力，流血、牺牲。1949年，为东土耳其斯坦共和国的建立浴血奋战的东土耳其斯坦各族将士们、共和国的各族领袖们，接受了苏俄及中共的假承诺。让中共军队和平进入到了我们神圣的东土耳其斯坦国土。当时，我们还有5万经历过战场洗礼的民族军将士。如果我们进行最坚决的反抗，可能东土耳其斯坦土地必然淹没在血海中。但维吾尔人的处境决不会是今天这样的悲惨结局！<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;我们维吾尔人热爱和平，我们不喜欢暴力。但如果敌人一而再、再而三地欺辱我们，挑战我们的底线。我们会义无反顾的奋起反抗。为将侵略者赶出我们的祖国，我们有权利采取任何的反抗方式。绝望中的民族是不会选择方式方法的，以极端回答极端是弱者的第一选择！<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;今次的阿克苏维吾尔人自杀式爆炸袭击“汉协军”，使中共‘汉协军’7死多伤。2008年喀什噶尔俩维吾尔人的自杀式袭击中共侵略军警，杀死16侵略政权军警，及2008年库车维吾尔人自杀式袭击。都说明了我当时说出了一个即将发生的事实。但中共一直没有将维吾尔人的警告当一回事儿，因为中共本身是暴力的崇拜者，中共的官员渴望血腥，那可以成就他们的仕途。<br />
&nbsp; &nbsp; <br />
