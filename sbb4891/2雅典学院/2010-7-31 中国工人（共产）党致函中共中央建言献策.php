&nbsp; &nbsp; 世界社会主义运动史从苏俄十月革命爆发九十二年以来，由于伟大导师列宁的早逝，社会主义国家历来只有一个共产党执政，列宁倡导的民主集中制原则因没能找到一种法制化、秩序化和组织化的有效形式和机制，使共产主义政党承诺的人民真正当家作主的民主初衷严重异化，一党专政及封建法西斯主义的复萌使共产党内的腐败不可遏制和人民民主得不到有效的保障，从而导致了世界上第一个社会主义国家――前苏联和东欧社会主义阵营的解体覆亡。四十四年前，中国人民的伟大领袖和导师毛泽东主席面对国际共产主义运动中出现的前苏联赫鲁晓夫修正主义逆流和中国国内的新情况，高瞻远瞩地科学预见了当今中国和世界上发生的一系列历史事件，在其古稀之年毅然决然地高举马克思列宁主义大旗，领导了史无前例的中国无产阶级文化大革命，发动亿万人民群众开始了社会主义国家政治文明和人民民主的伟大探索；上海工人阶级参与发起的一月革命风暴为无产阶级两党制提供了无产阶级专政（即人民民主专政）条件下社会主义制度自我完善的新鲜经验和历史教训。发生在上世纪六十年代前、中叶的国际共产主义运动总路线的大论战和历时十一年的中国无产阶级文化大革命培养了千百万革命事业的接班人，这是毛泽东同志和中国共产党对中国当代史和世界文明史的伟大贡献；正是这股革命力量在中共两位总书记犯下严重错误的严峻情况及以后多次出现的右倾危机中有效制止了中华人民共和国和中国共产党的解体覆亡，避免了重走前苏联和东欧的覆辙，保证了中国社会主义事业和现代化建设即改革开放的继续发展。这就是无产阶级文化大革命的重大历史意义之根本所在。正确认识无产阶级文化大革命的伟大历史意义，认真总结文化大革命的经验教训对于以共产主义政党为执政党的社会主义国家至关重要。社会主义国家如何拒腐防变、反修防修？共产党人如何永远保持与最广大人民群众的血肉联系而全心全意为人民服务？共产主义政党怎样才能始终不忘宗旨和使命而青春永驻？社会主义国家如何保证在生产力为决定因素的同时注意生产关系对生产力的反作用而又抓精神文明又促进物质文明和现代化建设（即抓革命从生产）使共产主义精神和道德观念深入人心？这些重大历史课题毛泽东同志和中国共产党人在文化大革命期间都进行了卓有成效的有益探索，获得了丰富的经验教训。把无产阶级文化大革命提高到历史发展的角度来正确认识，是当代中国人尤其是已经具备毛泽东思想之科学发展观的中国共产党人的明智之举。毛泽东同志提出的正确对待文化大革命，正确对待自己，正确对待人民群众的光辉论点至今仍有现实意义。<br />
<br />
&nbsp; &nbsp; 一年前，中国工人（共产）党第一次全国代表大会发表的《公报》指出：“无产阶级文化大革命的重大历史意义和经验教训应该重新得到评价，毛泽东同志的历史地位和毛泽东思想的权威作用应该重新确认。邓小平同志的历史作用之积极方面应该得到肯定，其消极方面也应该公布于众以正视听。把真实的历史告诉人民，不涂改、不抹煞历史就是科学发展观即辩证唯物主义和历史唯物主义。因此，在适当的时侯，中国共产主义政党应该作出一个《建国以后特别是现代化建设新时期以来若干历史问题的决议》，实事求是地把一些历史上的重大事件特别是无产阶级文化大革命的全部过程告诉人民：哪些是做了对的，哪些是做错了的，把文革前三年的‘天下大乱’和文革后七年的基本天下大治的真实情况公布于众，认真总结经验教训，客观评价历史事件。只有这样，才能最大限度地团结一切可以团结的力量，化消极因素为积极因素，以利于统一全国各族人民的思想，步调一致才能得胜利，才能真正达到构建社会主义和谐社会的目标。但是，要警惕一种倾向掩盖另一种倾向，要注意从一个极端走到另一个极端；要警惕有人打着毛主席的旗号干坏事，特别是那种带着红帽子藏着黑心肝的林彪一类政治骗子；要警惕那些只想为自己翻案而不顾人民群众死活的、证据确凿的文革‘打砸抢’分子沉渣泛起而扰乱左翼统一战线。”<br />
<br />
&nbsp; &nbsp; 无产阶级两党制的创立，继承和发扬了文化大革命的精髓，汲取了改革开放即社会主义现代化建设中的新鲜经验，为中国人民找到了一种政治体制改革的新模式。不以执政参政为目的而全心全意为人民服务、执政的和在野的两个共产党之间肝胆相照荣辱与共长期共存互相监督的崭新机制，正在成功地团结亿万人民群众构建无产阶级的社会主义和谐社会，创造性地补充完善了中国特色科学社会主义理论体系和共产党领导的多党合作和政治协商制度，解决了一党专政和资产阶级多党制混乱的矛盾。<br />
<br />
&nbsp; &nbsp; 为什么说无产阶级两党制是一种可行的社会主义民主模式？是因为无论是执政的还是在野的两个共产党都具备无产阶级的世界观，都不是以执政参政为目的而是以全心全意为人民服务为宗旨的。世界观的相同是两个共产党之间真诚合作和热情帮助的思想基础，这是共产党之间合作与其他小资产阶级民主党派合作的根本区别，也是之所以能够真正做到肝胆相照荣辱与共长期共存互相监督的根本原因。这种机制能有效促使两个共产党不断地改造世界观，不断地提升各自的政治素质，不断地与时俱进而跟上新时代的历史步伐。<br />
<br />
&nbsp; &nbsp; 不久前，中国工人（共产）党在上海召开了一届二中全会，全会在政治上、组织上进行了多方位的部署，对如何帮助兄弟的中国共产党执政为民，确保无产阶级的社会主义和谐社会构建和现代化建设即改革开放的顺利进行提出了一系列主张和意见，得到了广大人民群众的热忱欢迎和支持。<br />
<br />
&nbsp; &nbsp; 中国工人（共产）党进行的历时一年的整风学习取得了显著的成效，党员的质量在原有较高素质的基础上又有了新的提升。中国工人（共产）党是在无产阶级已经夺取全国政权，无产阶级专政（即人民民主专政）的社会主义制度已经基本确立，共产主义政党已经成为全国人民的领导核心力量的良好条件下公开成立的。这些良好条件为确保中国工人（共产）党党员的高质量既提供了各种便利条件，同时又提出了更新的时代要求。“宁要好梨一个，不要烂梨一筐。积极肯干和忠心耿耿的人即使只有两三个，也比十个暮气沉沉的人强。”这是伟大导师列宁对共产党人的要求。我们应当永远牢记列宁的谆谆教导，力求做得更好、更完美。共产党人就应该像雷锋、白求恩、张思德、王进喜、孔繁森、王瑛那样毫不利己专门利人，绝不能患得患失自私自利。共产党人要努力做“一个高尚的人，一个纯粹的人，一个有道德的人，一个脱离了低级趣味的人，一个有益于人民的人。”这是共产党的宗旨和新时代的基本要求，是共产党人先进性的根本体现，是共产党人能够带领广大人民群众完成历史使命的必备条件；做不到这一些还不如自行退出历史舞台。毛泽东同志提出的无产阶级革命事业接班人的五项条件是中国工人（共产）党党员的必备条件，共产党只能是工人阶级先锋队的性质是中国工人（共产）党具备了以工人阶级为先进生产力代表的合格要求。有了这些高标准严要求，中国工人（共产）党就能够避免兄弟的中国共产党曾犯过的错误，也能够帮助兄弟的中国共产党真正改正正在逐步认识到的把马克思列宁主义毛泽东思想边缘化而导致精神文明建设失衡和中共党内思想混乱的严重错误，更能够作为参考比照对象不断触动兄弟的中国共产党永远保持先进性而使其真正成为全中国最广大人民群众心悦诚服的无产阶级执政党。<br />
<br />
