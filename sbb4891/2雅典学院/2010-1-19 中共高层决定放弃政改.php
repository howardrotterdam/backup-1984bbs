													<h2>中共高层决定放弃政改</h2>
						
						
																											<div id="postmessage_440913" class="t_msgfont">去年中期以来，体制外民主力量对中共左派否定普世价值的行为进行了纵深批判，以至於高层智囊不得不藉“大外宣”之力对外宣称仍有政改意图.如二○○九年十一月五日，《俄罗斯记者》周刊发表其国内作者文章，称：“中国准备实行党内领导选举制，而不再单由上级任命。这将打破中国不会改革政治体制这一流传已久的神话。”<br />
<br />
对於此文，中国国内的资深政治分析人士大多认为其背景完全来自中共高层。因为，它不仅通过惯用的“出口转内销”方式由国内官媒译介，而且连基本表达方式都有抹不去的中共高层色彩。<br />
<br />
保护公众最不满的领域<br />
<br />
与这个表达背景相重？，中共政改理应着手司法与执法方面，但是两者均不见任何动静.相反，司法过程中玩弄文字游戏以侮辱公众智慧，执法过程则暴力开道以谄媚资本势力，均呈现常态化。前者以浙江两名协警轮奸入住宾馆女子案为典例。按中国法律，二人起步应判十年，最高可判无期，但地方法院创出新术语，称其行为是“临时强奸”，从轻判处三年徒刑（后鉴於公众及舆论之压力，终以“轮奸罪”改判十一年）。类似的还有北京发生的安徽上访女被当众强奸案，案犯亦被从轻判处八年徒刑，而当众强奸案一般是对案犯判无期乃至於死刑。轻判原因仅仅在於受害者是“国家的敌人”──上访者，在法官看来理应受到某种报应。<br />
<br />
“临时强奸”的判词之丑陋，连香港亲共媒体都看不过去了，乃发谏言称：“当事法院其实是在玩弄文字游戏，对司法公正进行一次明目张胆的亵渎.”此言虽然出於忠心，但是大陆司法不公即政治体制积弊最深的领域，却难以改革，其间的利益集团之蛮横已经完全合法化。因此，参加过数部法律制定的体制内高级法学家李曙光，对记者无奈地说：“中国的选择性执法、选择性司法的现象非常严重。一些不完善的良法没人实施，而一些恶法大家都打着依法行政的幌子，实行起来又快又有效率……。”<br />
<br />
说到执法中的暴力行为，从警员怒揪“小姐”头发被迫裸体拍照到拆迁队逼业主自焚，不一而足。在公众看来，执法的暴力行为比违法行为如“小姐”卖淫更危害社会，就不用说自焚业主是以生命来维权本无过错了！然而，正是公众情绪的集体表达，令中共高层十分头疼。在重判着名异议作家、网络撰稿人、维权精神领袖刘晓波之前，公安部长孟建柱发表文章，要坚决打击网络上的敌对势力；在刘案一审宣判后，孟的可能接任者、公安部常务副部长杨焕宁发表内部谈话，声称：“把握网上网下两个战场，全力维稳。”<br />
<br />
今年当局维稳压力极大<br />
<br />
杨焕宁的内部讲话罕见地重提“打击法轮邪教组织”。观察家认为这是二○○五年十月中共十六届五中全会以来第一次公开重提法轮功问题，表明胡温四年多来低调对待法轮功的政策遭到党内严肃批评.批评的原因据说有三：其一，法轮功在境外多国起诉江泽民，得到所在国法院的支持，在国内产生了冲击作用（外交部没敢正面言明起诉事件，其发言人只称“外国政府支持法轮功的做法是错误的”）；其二，江系势力把持政法系统，绝不允许任何为法轮功翻案的动向出现；其三，可预见二○一○年经济形势极为严峻，经济危机导致社会整体骚乱的可能性大增，而法轮功被认为是最有力的骚乱推手。<br />
<br />
以上第三点，可以对胡温一派形成巨大压力，因此，杨的内部讲话在高调指斥法轮功之前，先给定了一个前提，即把全国公安机关的工作与中央经济工作会议精神牵强地挂起？来。官媒舆论导向很积极地配合政法系统的宣传：先是新华社《参考消息》於二○○九年十二月二十日，罕见地将美国《新闻周刊》对中国二○一○年经济崩溃的预测转译过来；而后三天，国务院新闻办所属的《中国网》再次重提美刊预测.如此举动的目的在於向全社会暗示：经济形势愈加严峻，政治改革已无可能。<br />
<br />
一直以中共谏友身份评说中国事务的境外学者，也看到中共近期乃至更长时间无政治改革的可能，如有名的新加坡国立大学东亚研究所所长郑永年，曾於上月初撰文称：“如果在经济自由和政治民主方面中国很难作为，那么就要在其他方面有所突破。”郑氏所指是说中国应在环保经济方面争取国际话语权，但是哥本哈根全球气候峰会的结局说明中国在这方面不太能争取到空间，而国内经济自由倒退、政治民主无望却是冷峻的事实。<br />
<br />
西方政经幻想渐归破灭<br />
<br />
全球危机加剧了国际贸易冲突，随之带来了价值观对立的显性化。对於刘晓波案的人权呼n，无论是西方世界还是联合国，都没能影响中国的政治裁判。与此同时，中国通过订单外交大大地教训了欧洲的人权旗帜国家法国，因此，法国总理菲永访华时回避了人权问题与西藏问题.这说明西方曾幻想通过经济交流来改变中国政制的战略已不切实际，而刘晓波案的重判又将这个幻想彻底击灭。不仅如此，中国内部也一直在抱怨“只有大国责任，而无国际领导权”的处境，强烈希望改变当今世界格局。<br />
<br />
在全球领导权的分配方面，中国设定了一个内在的逻辑：凡是干预中国取得此项权力的力量，一律被视为敌对势力。因此，这个可怕的政治概念已不是专指流亡海外的中国异议精英，也涵盖了西方所有国家支持中国民主化的政治派别.正是明t这一点，台湾的马英九政府才在自己“先经济后政治”的主线下，没敢对刘晓波案作出明确反应，远不及马英九在野时“平反六？四”的高调了。<br />
<br />
中央党校被指自由化大本营<br />
<br />
中共体制内的一些开明学者一直在主张“党控民主”，其要点是：一要真正用党内民主如党员直接投票，来促动社会民主；二是提高党内知识精英的自由度，来推动政治理论创新。而且，按中央党校知名教授王长江的个人着作《政党现代化论》所披露的有关细节看，此类言论在胡锦涛任校长期间确实得到容忍，尽管批评声音一直不断。现在，随着左风渐盛、政改停顿信号强化，中央党校已被左派指责为“资产阶级自由化的大本营”。<br />
<br />
