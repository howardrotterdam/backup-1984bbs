																											<div id="postmessage_624912" class="t_msgfont">作者：黑夜里的牛<br />
<br />
　　什么自由是只有少数资本家和有钱人拥有？ <br />
　　追求幸福的自由？表达自已思想的自由？免于恐惧的自由？ <br />
　　如果劳动者有获得可能成为资本家的自由，这是不是自由？ <br />
　　-------------- <br />
　　当你每天要上十个小时班而且一个月就一天休息日的时候，你会发现，你跟你的老板的自由是两码事。 <br />
　　自由首先是人的自由，如果一个人把自己成年期甚至童年期的大部分时间出卖给别人，你觉得此人自由么？时间是人最宝贵的财富，是自由得以呈现的场所。如果时间被卖出去了，那么，自由本身能够存在于何出？ <br />
　　好吧，你给我言论自由，给我免于恐惧的自由了。那么，我想说的是。第一、言论自由本身需要表达言论的工具，比如，报纸、电视台等等，这些媒体背后都是利益集团在支撑，如果你想发表与这些集团的利益相违背的东西，他们肯让你发？第二、你可能会说现在出现网络了，但说句实话，能够大范围影响普通群众（不是自由派，也不是共产主义者或者法西斯主义者）观点的网站，恐怕也就些输得出来的几个吧。 <br />
　　 <br />
　　接下来“免于恐惧”。免于恐惧的自由是什么意思呢？这句话该怎么理解呢？也就是说，一个人可以选择“免于恐惧”，言下之意就是在某些国家，人们没法选择是否要“免于恐惧”。但是，我想问一句，你的恐惧来自什么地方呢？“免于恐惧”也就要求消除造成恐惧的成因。实际上，恐惧不仅仅来自国家暴力、或者暴民；恐惧也可以来自资本家、来自黑社会、来自持枪的少年儿童、来自黑手党和布什。所以，经过这些分析，我们可以发现，免于恐惧本身说不上什么选择，跟免于饥饿一样？一个暴政或者被推翻，或者就在暴政之中。（我这里仅仅把恐惧限制在国家暴力这里，这仅仅是为了分析的简便。）在暴政中，意味着“人人自危”；而消灭暴政的社会，人民可以“免于”恐惧。但是，我们发现，从暴政到暴政的消灭，这是一个选择的过程；但是，暴政本身并不能被选择，因为暴政本身就意味着对人民选择权的剥夺。所以，经过我们分析，在某种情况下，人们可以免于恐惧，而在另外一种情况，人民则必须恐惧。如果第二种情况出现，那么人民将没有选择权。这就是说，“免于恐惧”的自由本身并不存在。或者恐惧，或者没有恐惧。 <br />
　　 <br />
　　 再谈谈那个老掉牙的故事吧！工人勤俭致富，成为了举世瞩目的资本家。好吧。即使这个故事是真的。那么，我们想问一句：在几亿工资不到两千的工人阶级中，有几个人能积累起足够去剥削别人的资本？也许你会说，可以先当小店主，然后转型资本家。那么，我再问一句：在中国几千万（数字不详，这里先这么说）小店主中，有多少能成为资本家？不要认为资本家的资本是钱，工人的工资是钱，就认为他们是一样的东西。告诉你，不一样。工人的工资，是用来购买生活资料的。而资本家的资本，则是用来购买生产资料和劳动力的，前者花了就没，而后者可以像滚雪球一样越滚越大。所谓的两极分化、马太效应就是这样形成的。我这样说并不是否认白手起家的富翁的存在，毕竟，也有人通过中彩票、开赌场、卖毒品白手起家，也可以从国企厂长经理变成私企老总，方法很多，路子很广，就不必我来评说了。也许总有那么一两个善良的白手起家的工人在埋怨我们打击范围过大，但是，如果让他看看周围和他一样白手起家的人，也许他就会理解我们的怒火了。</div>

							
							
							
							
