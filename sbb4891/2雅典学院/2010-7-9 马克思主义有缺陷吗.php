马克思主义有缺陷吗?<br />
<br />
 <br />
如果,我们用一种全面彻底系统长远正确的要求去检测马克思主义,或者用一种非常理想完美的要求去检测马克思主义,那么,马克思主义是不是也存在着错误或者不足?<br />
<br />
慈天元&nbsp;&nbsp;发帖：<br />
<br />
<br />
马克思的世界观采用的是单因参量──物质一元化，这种世界观有着天然的排它性：对于能量和信息的同构作用没有充分考量。<br />
<br />
马克思看到了阶级和阶级压迫，提出“无产阶级专政”才是人类社会的出路。不错，人类社会的确有阶级存在，也的确有阶级压迫现象的存在，但是简单的用“无产阶级”取代“资产阶级”根本就不可能是什么“出路”。<br />
<br />
出路在于打破阶级的稳定性，使下者有上升的通道，使上者有失势的可能──民主政体正是为此而设计的。下层阶级的上升，依赖于自身影响力的增强;上层阶级的失势，则因于其对社会普遍意志的违背。上层阶级的存在不是天然罪恶的，它的罪恶只在于它触犯了最广泛人群的利益。<br />
<br />
别说社会，就是人体，也是有阶级属性的：人的神经系统、消化系统、循环系统……不就是天然的阶级吗？你说神经系统压迫了消化系统，要用消化系统取而代之，可以么？系统的协同显然比阶级仇杀更重要。 <br />
<br />
马克思看到了资本原生代血腥积累的过程，他想终结那个过程，这个想法无疑是对的。但他的一元化思维模式限制了他的头脑──他没有想到过资本主义的中生代和新生代会是什么样子，他只认为资本主义应该被彻底的终结。<br />
<br />
马克思知道事物都有发展变化的过程，但他的哲学，只许自己的派别发展改进，而不许自己的对手也有改进的空间──他从没想到过自己的对手也有同样的权力。<br />
<br />
我党以马克思主义为自己的立党之本，也就继承了马克思的错误，为什么我党不允许别党主政？这同马克思主义的一元化哲学不无关系。马克思主义是一种逻辑体系混乱的主义，它的理想、目的和手段根本就不能统一起来。<br />
<br />
