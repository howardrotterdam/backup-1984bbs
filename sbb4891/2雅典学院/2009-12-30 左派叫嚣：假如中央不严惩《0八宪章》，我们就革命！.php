<br />
　　看了司马南：《“中国颜色革命宣言”背景一则》一文，文中揭露《零八宪章》的实质内容使我这个不喜欢动笔的懒人内心久久无法平静。第一反应是反社会主义反人民的屠刀已经悬在我们的头顶。一场你死我活的决战已经开始。不过，“西山会议派”们的罪证也已经充分暴露。他们已经构成《颠覆国家政权罪》，《推翻社会主义制度罪》。凭《零八宪章》这一证据已经足以认定。其触犯的法律条文就是《中人民共和国刑法》第一百零五条。现在看该条的具体规定：<br />
<br />
　　中人民共和国刑法<br />
<br />
　　第一百零五条&nbsp;&nbsp;组织，策划，实施颠覆国家政权，推翻社会主义制度的，对首要分子或者罪行重大的，处无期徒刑或者10年以上有期徒刑；对积极参加的，处3年以上10年以下有期徒刑；对其他参加的，处3年以下有期徒刑，拘役，管制或者剥夺政治权利。<br />
<br />
　　以造谣，诽谤或者其他方式煽动颠覆国家政权，推翻社会主义制度的，处5年以下有期徒刑，拘役，管制或者剥夺政治权利；首要分子或者罪行重大的，处5年以上有期徒刑。<br />
<br />
　　椐此，可以看出“西山会议派”们已经构成《颠覆国家政权罪》，《推翻社会主义制度罪》无疑。他们无论是组织，策划，实施或者是煽动，都已经做到了。组织，策划，实施--他们有纲领，且已经纠集组织300余人（注：已经在《零八宪章》上签名的有143人）；煽动--他们已经在互联网发帖且已经被美国翻译为外文。他们为什么会在互联网上公开帖上《零八宪章》全部内容？我对他们的目的试做如下分析，不全面的或者分析错了的欢迎同志们补充纠正。<br />
<br />
