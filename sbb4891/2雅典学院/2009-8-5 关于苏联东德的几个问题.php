													<h2>回复 24楼 luugoo 的话题</h2>
						
						
																											<div id="postmessage_197443" class="t_msgfont">我尝试着说说吧，具体情况可能有些出入，见谅<br />
<br />
1.那中国加入WTO，融入（如果有可能的话）世界市场经济的时候，这些企业在世界上的其他国家（意味着失去了政府这个靠山）会怎么样呢？<br />
呃，这么问吧，比如海尔，反正在国内是宣传的很好，但是在美国欧洲或者其他发展中国家，现状是怎么样的呢？<br />
<br />
&nbsp; &nbsp;中国加入WTO，有一种标志性的影响，也就是生产地的又一次转移。回顾二战后历史，您可以发现，首先是60年代欧洲和日本复苏的完成，然后是70、80年代的“亚洲四小龙”的起飞，但四小龙所达到的程度，已经明显不如欧洲和日本了（最明显一点，日本尚可加入G7），90年代以后，苏东剧变，中国改革，释放了原先被禁锢和浪费在军工领域的共产国家的市场和劳动力，外加IT技术发展，造成了欧美从90年代开始一直到07年的大繁荣。对东南亚而言，90年代末的东南亚金融危机是一个劫难――这个&quot;劫难&quot;，在我事后臭皮匠看来，即使不以金融危机的形式爆发，迟早是要用别的形式爆发的――国际范围内的大规模的廉价产品生产地，已经从四小龙和东南亚开始往中国大陆转移，而四小龙又没有本事学习日本，向产业链上有方向“升空”，因此，四小龙的悲剧几乎是注定的。随着911发生，美国战略重点转移，于是中国的入世，成了这种转移的“标志”，一直到今天。<br />
<br />
而在国际市场上，竞争相对比较公平（我是说经济方面的），同样的产品，质量能满足要求的，当然是选价格便宜的。因此，中国政府对国内企业（无论国有民有）最大的支持，就是维持国内的低工资，低环保成本的现状。这才是中国政府最大的支持。只要中国的工资最低，污染控制最放松，低利率，税收、法规等倾向于鼓励出口，原料、运价便宜，那么按照资本的逐利本性，必然会来投资的，而国内资本也可以借此在国际市场上盈利（当然，既然涉及了低工资，那就必然是相对低端的产品），同样，是否可以维持这种低成本、低工资――当然，还有低汇率，那是另一件事情――这牵扯的面更多，比如最近抓澳洲力拓间谍的事情，说到底，也是一种维持低成本的手段，至于有效无效，那是另一件事情。<br />
<br />
&nbsp; &nbsp;具体企业的事情下面说吧。<br />
<br />
2.前些日子看报纸说，要国内大型企业警惕在国外设厂地的罢工，还谴责他们不知好歹，一味索取。这是不是说明，我们这些垄断国企到了国外，一没有人文精神，二没有劳资和谐的意识，三不懂法律？也就是说，除了国内的资本，其实他们和sony、松下这样的企业根本不是一个等级的，是吧？估计几十年内都没指望。<br />
<br />
在国外设厂，除了一些文化、经济、法律上的阻碍，最直接的阻碍是您说的劳资关系，最现实的问题就是工会。现在出去的中国企业，原先在拥有的几大优势基本上在国外不起作用，比如，廉价劳动力的优势，会受到工会阻遏；环保、污染方面的优势，会受到国外相对严密的立法执法方面的控制；政府管制方面的优势，也和国内的情况不一样，所以“水土不服”是情理之内的。我听说过一些数据，对外投资的现状是很不乐观的，而更多项目，或者说能够长期维持的项目，仍旧是以“援建”的“政治任务”――比如华为等亚非拉造移动基站，投资开矿之类，即便如此，掣肘仍不可避免，大如优尼科收购，黑石的投资，往往折戟，至于联想收购ibm，那事情就更摆明了（悄悄说下：即便Namibia这种坛眼里不入流的“小国”做也就几千万的小生意，最近不也出事了嘛）。中国制造在国外，仍然不免“廉价”、“低质量”的名声。我当然相信有一个或者几个中国企业能够在国际市场上弄的很好，不过就我所知，无论国际、国内市场，家电、成衣、IT等产品的高端品牌（或者说价格大的品牌）仍然是国外的：以海尔赖以起家的冰箱为例，至少我在上海看到的，海尔就不如n多类似西门子、LG 、伊莱克斯等贵，而惠而浦折戟中国多年，今年又在中国推出n种冰箱，可见海尔实力到底多少。<br />
<br />
3.假如国内政局有变，他们的靠山不灵了，或者乱套了，他们这些大企业也就会不行了吗？会被外企占据市场吗？<br />
4.苏联、东德当年也是咱们目前的情况吗？我记得有几个东欧国家后来也搞了市场经济。那这些垄断国企最后的结果是什么呢？<br />
<br />
这两个问题我尝试着一起回答吧。<br />
国内政局有变，国内企业会不会受影响。我的理解是：是国内政局其实一直在变，只是变大变小的问题。企业主比我辈聪明的多，拥有的人脉资源也比我们想象得要多，自然也能化解那些小的政局、人士变动的风险。如同我在上文所说那样，只要维持低人权、低成本、低汇率以及稳定的大局不变，那么，就算企业有了经营上的风险，并最终失势，那也很快会有新的企业顶替他们的位置，比如，很多年前的春兰，长虹、科龙等，各领风骚几年，如今又换新颜。换的是企业，品牌，不换的是低人权，低成本，低汇率。<br />
<br />
苏东的情况和我们相同的地方，比如官僚、国有、低效率等暂不去说，最大的不同是，第一，封闭，第二，偏军工、重工。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;封闭的影响可能更大。苏联对外开放几乎比我们还晚，我可能会记错，不过我记得好像87年以后苏联才有与西方国家的合资工厂之类，类似我国的“经济特区”也是在80年代末才有的。他们很奇怪一点，虽然相对而言，制造业放开得晚，但金融业却开放很早。本朝银行业相对开放，深发展、招商银行是在87年以后成立，而且说到底，至今都是国家严格控制，但苏联银行87年代也已经开放了，而且更重要的是，当时苏联对民间金融、外资金融的控制远不如本朝有力――在90年以后尤甚。我有一个没有出处的数据，或备一说吧：苏联早在勃烈日涅夫时代的1980年前后，苏联国家银行控制的卢布大概也只有总地下钱庄控制卢布的60%――没有出典，这个表述可能也有些问题，不可全信――但以此约略说明下苏联地下金融之厉害。<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 因为银行相对于企业的优势，所以苏联银行在企业弱势的时候，对企业的控制是相对容易的。而当苏联解体之后，国有化变私有化，国民每个人都发私有化债券，而唯有银行有办法把这些私有化债券集合起来，收购企业，形成巨大的财团――这个就是俄罗斯“寡头”。至于叶利钦如何与之周旋，普京如何强力摧毁，那是另外的故事了。（东欧国家变革道路各有不同，恕不详述）<br />
<br />
&nbsp; &nbsp; 中国不一样。中国对外开放早，欧美外资渗入早，渗透的面也比苏联当时广泛很多，一旦有所“不测”，我觉得外力会在中国发挥比当年在苏联更大的力量。但这个要考虑中国到底何时不测，如何“不测”，那题目很大。至于外企是否会占领市场一点，我觉得这个问题并不重要，毕竟，“强盛”如今天，外资也没有占据中国市场，中国人的购买力也买不起那么多国外产品，到了“不测”之后，购买力应该更低下吧，肯定还是三鹿之类的产品销路比多美滋、雅培要好。在企业是否为外资控制方面，变数更多，包括国内、国际政局等，我更不敢断言了，不过我猜“不测”之后对央企的争夺与瓜分，从戏剧的角度说一定很好看。当然，这都是建立在“不测”的基础上的，权当子虚乌有吧：）<br />
<br />
5.雷曼你的经济真厉害~~<br />
