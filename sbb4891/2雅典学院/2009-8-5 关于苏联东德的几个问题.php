&nbsp; &nbsp;&nbsp; &nbsp; 回到关于竞争力的方面。中国企业在寻求政府的支持方面的能力，肯定是全球独步的。无论是身处垄断、半垄断的中石油石化，各银行，移动联通宝钢等，以及相对中、下游的长虹、海尔、春兰，都与各级政府关系密切非常。即便三鹿、蒙牛这些几乎与垄断无任何关系的最低端的企业，都与当地或者某地、中央某部委等关系非凡――最可笑的是，即使炒菜锅、果汁饮料，都是关系国家产业安全，收购意向会因为影响国家安全而被叫停。所以说，在这点上，中国的企业竞争力是极强的。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 但这种政治领域内的超强竞争力也导致了企业经济领域内竞争力的衰弱。这个题目很大，包括私有制，经济效率等多方面的问题，以后有空可以说，但这里只需简单说：正因为政治方面的竞争力太强，得到的好处太占主导优势（dominating）了，所以企业自然会放弃在经济方面的竞争力――因为经济方面的竞争力在政治面前几乎不值一提。对一个国内企业而言，最重要的不是经济能力，不是产品是否适应市场需要，而是与当地政府的关系――以及到达一定规模之后，与中央政府的关系。而企业主――无论是所谓的央企或者国有企业的头，还是民营的老板，与当地政府的人脉影响，也是他们最关注的一点。这种关系，与“地方保护主义”等有着千丝万缕联系，如果要举具体的典型的话，可以看看全国有多少汽车工厂就可知道，各地保护有多严重，而全国有多少啤酒的品牌，也可以管窥一下其中奥妙。<br />
