根据货币主义的观点，巨额外汇储备形成的原因及传导链是僵化的汇率制度（人民币长期被低估）――出口产品相对价格低――外贸顺差――外汇储备激增――基础货币投放过多――利率下降――投资过热――产能过剩――寻求外部市场――人民币低估（人为的）――出口产品相对价格低――……<br />
<br />
具体到中国的外向型经济体身上，就存在汇率制度和国内产能的双向失调和畸形的相互依赖，也就是在官方“引导”经济增长的情况下，极难通过汇率制度的改善（如许多学者呼吁的通过人民币升值来达到内外均衡）来实现国内经济增长模式的转变， 这种转变主要受以下几方面的掣肘：<br />
1、内需不足，这个是老话题了，见诸报端的都是一些老生常谈的观点，实际上，只要从金融危机背景下大陆奢侈品市场的增长就可以看出，中国上层人士的购买力还是极惊人的，这主要涉及到收入分配机制的问题，即无法从正常的市场经济中获得正常的工资、利息、租金、利润收入（个人最近一直在考虑中国的市场与成熟市场机制的区别，觉得从“消费者主权”的角度破解似乎可行，望高手指点），加上事实上最有活力的中小企业的较高的税负和交易成本，囊中羞涩的情况下实在不敢消费，而实际上，“贵国”存在的种种风险与不确定性使得人们更有动机去储蓄而非消费；<br />
2、官僚体制的效率，这个问题属于所有社会主义阵营经济运行的通病，不管是技术官僚还是行政官僚在市场体制下均存在严重的资源错配和委托代理问题，这个应该是公有制的死结，相关内容可参见奥地利学派的有关著述；<br />
