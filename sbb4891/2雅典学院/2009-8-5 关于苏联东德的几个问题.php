													<h2>关于苏联和东欧经济，简述如下</h2>
						
						
																											<div id="postmessage_192715" class="t_msgfont">在60、70年代以后，华沙条约内部关系基本已经理顺了，南斯拉夫和苏联的关系也已经温和下来，这个时候苏联、华约国家（外加南斯拉夫）以及西方自由世界的经济关系，基本上是这么一个模式<br />
<br />
苏联的石油、原材料、贵金属出口――》西方<br />
西方的高科技工业品、民品和粮食――》东欧和苏联<br />
苏联的重工产品――》东欧<br />
东欧的轻工产品、初级产品、农产品、粮食――》苏联<br />
（当然，还有一些次要的流动，比如东欧的一些农产品也出口西方，但这个实在太小，先不提）<br />
上面几个构成了一个经济循环（可以画一个简单的图吧，不过不知道这个bbs上怎么画）<br />
需要指出，这个循环对苏东国家是非常重要的，因为他们只有这一条经济链条，而对自由世界而言，这仅仅是可选择的一环，自由世界内部以及自由世界与其他地区，比如中国、非洲、拉美等等，都各有循环，不是依靠这个循环链的，更何况，在80年代以后，it业之类的产业革命重新起来了（比尔盖茨就是80年代发家的吧，我记得）<br />
<br />
到了70年代石油危机以后，尤其是80年代之后，随着世界局势缓和，苏联所以来的出口原材料、石油价格下降，给苏联的外汇进手带来很大的问题，而苏联的军工部门的军备竞赛和预算却始终小不下来――比较有名的就是喜欢和美国玩军备竞赛，造一堆没用的大家伙，这些东西造价贵，维护更贵，绝对耗体能的<br />
<br />
这样，上述循环的第一个环节出了问题，就影响到后面的的整个循环（对西方而言，80年代和阿拉伯世界的关系恢复了，没有苏联的石油也能过日子，所以影响不大，但对苏联而言，这种影响是致命的――尤其要考虑到苏联始终没有实现粮食自给）<br />
<br />
另外，加上阿富汗战争的拖累，以及所有国有企业，国有机制等不可避免的低效率的问题，低生育率、酗酒的问题，苏联的经济在80年代初达到顶峰以后，不断走下坡路，尽管数据上还有些变化，但我看过的一些材料表明，居民实际购买力而言在70年代末达到最高峰，后面一直是下坡路。<br />
<br />
所以到了苏联改革后期，上面那个循环是最终无法维持了。那么整个“生物链”上的国家――包括朝鲜――都倒霉了。</div>

							
							
							
							
