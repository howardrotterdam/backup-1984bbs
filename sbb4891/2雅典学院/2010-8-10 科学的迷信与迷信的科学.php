													<h2>科学的迷信与迷信的科学</h2>
						
						
																											<div id="postmessage_777258" class="t_msgfont">作者：田松<br />
<br />
　　在我们年轻时所受的教育中，科学与迷信是一对水火不容的死对头。科学昌盛，迷信就会不攻自破。如太阳高升，霜露自然无形。科学之神所到之处，迷信之鬼便该悄悄溜走，但我们偏偏看到算命先生用上了计算机，名之曰科学算命。与几十年前相比，中国民众科学素养的绝对值不知要提高多少，但科学与迷信是否有此消彼长的关系，大可怀疑。<br />
<br />
　　怎么样才叫迷信，需要定义一下。<br />
<br />
　　有人把宗教等同于迷信，不但宗教家不能同意，科学家也不能同意。爱因斯坦说：“没有宗教的科学是跛足的，没有科学的宗教是盲目的。”尽管很多迷信都与宗教有关，但是宗教并不必然等同于迷信，相反，很多宗教是反对迷信的，至少在名义上是这样，比如基督教就曾经烧死过许多女巫。同一件事情，有的人认为是迷信的，但是信的人却不认为自己在迷信着。<br />
<br />
　　也许我们应该把迷信的态度和迷信的对象区分开。所谓迷信的常常表现为对不能把握的力量和不能理解的事物的狂热崇拜，因而超自然的神秘力量常常成为迷信的对象，比如特异功能和外星人之类。但也有些对象被一部分人所迷信，被另一部分人视为平常。一个现代城市人谁也不会感到奇怪的可口可乐瓶，在美国电影《上帝发疯了》之中，被一个飞行员扔到了非洲丛林，就弄得丛林中的土著困惑异常，先认为是上帝送来的，后又认为是魔鬼派来的。这个可乐瓶便成为当地土著迷信的对象。有时，迷信也可以解释为文化冲突。一个有经验的农民，能够预知第二年的收成，在他自己看来，自然而然；在自以为掌握了科学的人看来，则可能认为是迷信，因为这个农民进行判断的理由和依据不是来自科学，而恰恰来自科学之外。当然，最常见的迷信对象是宗教中的神灵，相当多数的教徒对所信仰的宗教所持的态度正是迷信。比如庙里求神拜佛的芸芸众生，我可以说他们迷信，但是我却不敢说东坡居士玄奘大师等人对佛教的态度也是迷信。<br />
<br />
　　所以对于很多事情，比如鬼神、星座，对于很多行为，比如烧香、念佛，是否是迷信，不可一概而论。迷信与否，很像米卢的口号，态度决定一切。比如某些人对文体明星的追逐，可以解释为迷恋、爱慕、敬仰，也可以解释为迷信。<br />
<br />
　　但是，让很多人不愿接受的是，科学，也常常是迷信的对象。<br />
<br />
　　虽然从理想上，我们会说，科学不承认权威，只承认事实。但实际上，科学本身已经成了最大的权威。一个科技副县长在向农民推广化肥时，他会理直气壮地说：“这是科学。”尽管他可能不懂化肥的化学细节，他也会底气十足。就如红卫兵在抄家之前高声念诵：“毛主席指到哪里，我们就打到哪里。”虽然毛主席究竟怎么想的，他们根本就不知道。但是他们却自以为掌握了天机，真理在手，便有了为所欲为的胆量。就如当年没有人敢于反对毛主席一样，现在，也没有多少人敢于反对科学。科学殿堂这个常用词表明，科学在大众中的形象恰恰是神！<br />
<br />
　　把自己不懂的东西尊奉到一个至高无上的位置，这种态度，正是迷信。<br />
<br />
　　很多人，尤其是科学信徒希望消灭迷信，以为消灭了迷信，科学就会兴旺，就像有些人以为消灭了细菌，人类就不会得病一样。又如很多人简单地把疾病归之于细菌，也有很多人认为，迷信是由于无知，所以普及科学可以消灭迷信。然而，有知到什么程度才能不迷信呢？吾生也有涯，知也无涯。无论如何有知，相对于复杂的宇宙和人类社会，都是沧海一粟。既然无法全知全能，人就永远有未知的神秘的东西，许多科学家如开普勒曾经是兼职的占星家，现在的占星家在西方国家也经常出入政要之府。这表明迷信与知识多少并无直接联系。<br />
<br />
　　迷信既然是一种态度，就该从心理上找原因。<br />
<br />
　　人是脆弱的，需要一种比自己强大的东西可以依靠，使人在面对选择与等待时减少惶恐。比如一个只懂物理学不懂关系学的副教授，在等待连续与关系到他的工资福利住房和妻儿老小的命运的教授职称失之交臂的时候，如果有人告诉他一个什么大师可以预测，他有多大几率彻底摆脱这种诱惑？当人们对未来无从把握时，难免会相信某种神秘的力量。在人们需要大师的时候，大师就会出现。这时，不迷信的反而是看破红尘的老僧。<br />
<br />
　　迷信的人很少认为自己在迷信着，而认为自己在相信。事实上，我们也很难区别，科学家的信仰与迷信者的信念在心理上有什么区别。爱因斯坦相信上帝不掷骰子；一个农家老妇相信电闪雷鸣出自雷公电母，同样是信念，我们能否从心理状态上区分，这个是迷信，那个是科学？同样，凭什么我们应该相信，科学必然能够给人带来幸福？这种对科学的信念与一位农妇坚决地相信菩萨的保佑，哪个是迷信，哪个是科学？<br />
<br />
　　当现代化的伐木工人进入长江中上游时，当地人可能会说出若干不能砍树的道理，比如触怒山神树精，给人带来灾祸之类的，从科学的角度看，当然是迷信。<strong>只有被现代科学武装起来的头脑和技术，才能够并且敢于剃光一个个山头，并把这视为对自然的征服。这难免使人产生困惑，为什么那些落后、迷信的风俗保住了环境，而使人战胜了自然的科学却反过来使人陷入了困境，这是不是因为我们对科学过于迷信了呢？</strong></div>

							
							
							
							
