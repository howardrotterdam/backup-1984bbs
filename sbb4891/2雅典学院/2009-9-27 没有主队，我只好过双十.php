																											<div id="postmessage_260759" class="t_msgfont">我是球迷，但除了特争气的国家队，不曾有过主队。<br />
<br />
&nbsp; &nbsp; 原产地河北，“球”队没进过乙级。天津上学，凑热闹不甘心。好友是沈阳人，辽宁队铁杆儿，那时辽足厉害，胡乱眉飞色舞。来京二十余年，看球迷热情投入，听国骂不堪入耳，亲临工体多次，都能坐怀不乱“理智对输赢”，只怪国安不是主队。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 国庆也这样，八竿子打不着。赶上有限几回，从未自作多情。三十五周年，南开集体舞献媚，没捧场，有人动员，有人遗憾，不失落；四十周年，刚平息还在戒严，没想通，不光临非法集会，也没人邀请，不心烦；五十周年，他改变了中国，咱去石家庄躲清静；六十周年，只盼冒烟儿的国庆早点儿完事儿，让他过完瘾，大家该干啥干啥。不想一回来就让人看死了――不是送请帖，是落实咱究竟会在哪儿。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 国家是谁的？你家的？还是毛新宇家的？老百姓的？还是组织上的？七千万的？还是九个主儿的？征服者的？还是亡国奴的？都得想清楚：国家是人家的！喜事儿丧事儿，都是人家的！死人该降半旗的，但乐意降到底，照样不关你的事。所谓国庆，无非是人家选个日子，让张艺谋把摄影棚搬到大街上，张牙舞爪穷凶极恶，显摆的就是横行霸道！可惜处处心虚没底――昨天上海虹桥，拔皮带提裤子过安检，亡国奴不过如此。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 姑娘小伙儿得动脑子：可以拿村长不当干部，别拿自己不当外人，往事终将不可回首！如痴如醉和假装如痴如醉，效果一样。大半天不吃不喝，裆里夹几十万条尿不湿，活活儿冒顿傻气，让人当兵马俑给耍了，将来你会觉得不值。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 国家不是我的主队，长街未必是你的主场，啦啦队不能太缺心眼儿。多余起腻，无需起哄，热脸贴冷屁股，招人烦让人疑。奉旨帮忙得筛选，砸中了算莫大荣光，祖坟上青烟闪过，但青烟属磷光鬼火，闪过也就闪过了――一亲戚十年前入选女民兵方队，踢完正步照丢饭碗――国棉三厂那口锅砸了。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 好在“时间开始了”，国庆终归要过去的。“一百年不动摇”，说话过了十好几年，还剩八十几年记不得了，但不信它能再过个六十整寿。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 别提前祝我国庆快乐，我既过中秋也过双十，唯独不过十一。那国家也未必是咱的，但同文同种百岁数儿大，俩花甲能赶得上，人家也能扛得到。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 浦志强 2009年9月27日 北京<br />
