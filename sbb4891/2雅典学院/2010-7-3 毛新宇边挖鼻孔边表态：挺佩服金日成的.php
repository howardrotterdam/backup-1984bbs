“我希望将来能造福人民”有人告诉毛新宇，他们一家在专业选择上与斯大林一家有点像。<br />
　　<br />
&nbsp; &nbsp; “哪些地方像?”他问。 　<br />
<br />
&nbsp; &nbsp; “毛主席和斯大林都是伟人、大政治家、军事家、革命家和理论家，而你的李讷姑姑毕业于北京大学历史系，和斯大林的女儿一样都是学历史的，斯大林有一个孙子是军事历史学家，你又是以历史为专业。斯大林的女儿说她的专业是父亲定的，不知你李讷姑姑和你的专业选择是不是由 ‘老人家’定的?” 　　<br />
<br />
&nbsp; &nbsp; “这么说真是有点像，以前我没注意。李讷姑姑的专业选择是不是爷爷定的，我不知道。我的专业选择是妈妈帮助定的，但爷爷的影响肯定是有的。” 　　<br />
<br />
&nbsp; &nbsp; 为什么学历史?今后想专门研究历史吗?<br />
<br />
&nbsp; &nbsp; 作为历史专业学生，毛新宇对故宫有特殊感情。一次一位朋友约他一起出游，他就要求去故宫，这次是他七逛故宫。来到午门前，毛新宇比比划划描绘昔日旧皇朝在这里举行献俘仪式的威仪。他模仿着禁卫军发出的吼声，指点着皇帝应居的位置，显然他对于在这里发生的历史典故极为熟悉，不愧为历史系的学生。他说他最喜欢逛故宫，今番虽然是七逛故宫了，但他还想再来，只要有机会。<br />
　　<br />
&nbsp; &nbsp; 来到太和殿 (即俗称之金銮殿)，他说他也听说一位相士曾给“老人家”算过卦，说“老人家”和他的子孙不宜到此，友人问他为什么还来，他嘿嘿一笑，说：“我不怕，我还要在这里留影呢。” 　　<br />
<br />
&nbsp; &nbsp; 毛新宇去过朝鲜两次，两次都受到了金日成主席的接见。谈起这段经历，他眉飞色舞。“金日成主席很尊敬我爷爷，也很关心我们一家，常邀请我们去朝鲜给我大伯扫墓。他12岁就开始干革命，20多岁就建党、拉队伍，32岁就当了国家领袖，非常不容易。我挺佩服金日成主席。金主席还很重感情，爷爷活的时候，每逢爷爷诞辰，他都派专人给爷爷送礼物，虽然不是什么贵重的东西，但礼轻情义重，那份心意真叫人感动。”<br />
<br />
&nbsp; &nbsp; “爷爷的儿女都是好样的”　新宇很骄傲地说。<br />
　　<br />
&nbsp; &nbsp; “爷爷长大成人的儿女只有大伯、父亲和两个姑姑，大伯牺牲在朝鲜，这是大家都知道的。父亲和两个姑姑虽然没有惊人业绩，但他们都善良、正直、清正、廉洁，都没有做过给爷爷名声抹黑的事，什么官倒之类腐败行为与他们无缘。爷爷的儿女都是好样的。” 　　<br />
<br />
&nbsp; &nbsp; 他说，他们一家与两个姑姑的关系并不像某些传闻所说互不往来。他近来多次随父母去看望两个姑姑。姑姑们对他非常好，虽然言语不多，但都很真诚、善良。他很同情李讷姑姑的境遇。这个姑姑背着江青这个大包袱，虽是母女，但江青活着的时候，要看江青一次也很不容易，看了之后也有诸多烦恼。 　　<br />
<br />
&nbsp; &nbsp; “她活得很不容易!”毛新宇叹道。<br />
<br />
&nbsp; &nbsp; 毛新宇的父亲毛岸青身体不好，对毛新宇的监护教育之责就落在了其母邵华身上。十多年前，邵华在著名散文《我们爱韶山的红杜鹃》一文中写道：“我们一定要让我们儿子新宇懂得，韶山的红杜鹃为什么这样红。”而今邵华对于毛新宇近两年来开始关心研究爷爷的思想和实践非常满意(看中国编者注:邵华已经去世)，母亲希望儿子能不断地充实丰富自己，提高马克思主义理论水平，为坚持、捍卫和宣传社会主义、毛泽东思想做出贡献。母亲邵华对儿子爱之深，责之切，常有恨铁不成钢之感，督责起来颇严厉，但她自知儿子某些方面有“病”，不得已也只好放弃。儿子对母亲的严教，也啧有烦言，有时竟至当众顶撞母亲，但一般情况下，儿子终被母亲说服，仍依母亲之意行事。<br />
