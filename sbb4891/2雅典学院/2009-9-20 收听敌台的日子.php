													<h2>收听敌台的日子</h2>
						
						
																											<div id="postmessage_249879" class="t_msgfont">我初中时，父上大人给我买了一台德生牌的短波收音机，当时的目的是为了提高英语水平。我现在还记得那个傻逼英语老师忽悠我们的话：“买台收音机哈，听听那个喂哦哎（VOA），会有好处滴。”因为我系班涨，她要我带头买。那台收音机有外接电源，不太耗电。<br />
<br />
起初，我就傻不拉叽找那个VOA英语广播啊，找不到。不过，却发现一个什么美国之音中文台，那就将就听吧。听鸟两天，感觉不对，什么玩意？怎么与电视新闻里说的完全不同？我就不听了。收音机被长卷箱底。<br />
<br />
9・11事件当天晚上，我好奇美国方面反映如何，于是又翻出收听。这下子，我完蛋鸟，被美帝彻底洗脑。渐渐，我就离不开那玩意了，当时像美国之音、BBC、加广、法广、德国之声，好像接力广播似的，听完这个，下个刚好开始。<br />
<br />
法广一般晚上听，正好放学回家，一边吃饭一边听，那时它还顺带放一些经典法语歌曲，真是好听极了。<br />
<br />
“这里是伦敦BBC”，主持人熟练的声音。我很喜欢那时BBC的连线采访，节奏明快，简单几句话就真相大白。<br />
<br />
德国之声听得很少。<br />
<br />
美国之音的听众热线之类的节目听得比较多，大陆的听众可以打对方付费电话参与讨论。很好笑的是，我经常听到一个老头变着法儿多次打，一会自称姓王，一会又姓李，一会又说来自广东，但是口音永远是东北的，现实中憋得太久了。<br />
<br />
自由亚洲电台不太听，也听不到，干扰太大，从那时起就夹杂着“民族乐吹打”，欢天喜地的样子。到现在，我一听吹打的就头疼。<br />
<br />
我很喜欢湾湾的中央广播电台台北国际之声新闻广播网，一方面信息比较全，其次它的评论也很实际。那时我做一个工作，就是快速记录其节目《历史上的今天》，每天5分钟，带我回顾历史---真实的历史。一年后，我足足记了两大本。很多人名、历史事件我都是第一次听说，像魏京生、金无怠、王丹之类。<br />
<br />
<br />
到了快中考的时候，父母怕影响我的成绩，偷偷的把它藏起来了。我翻箱倒柜都没找到。后来，我偶然在一个堆满杂物的抽屉里发现它，它早已腐锈不堪，天线也没了，按钮都掉落了。<br />
<br />
接着的三四年，我都没有这些敌台的消息。06年买了电脑，学会翻墙之后，我经常会会这些老朋友，下载一些音频听听。不过当初的那种感觉再也没有鸟。<br />
<br />
