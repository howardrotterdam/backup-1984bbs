至于你说《金刚经》，那我正好拿这经做个佐证，来说明一下大乘佛教经典对于历史的可靠程度（我是说历史可靠程度，不是说道理可靠程度）。当然，为了防止下地狱，我就不自己说了，下面转贴一文，让原文作者去替我承担地藏王菩萨的诅咒吧。:)<br />
&lt;&lt;&lt;&lt;&lt;&lt;<br />
《金刚经》的初期大乘之特色<br />
作者：达　照&nbsp; &nbsp; 文章来源：不详&nbsp; &nbsp; 点击数：37&nbsp; &nbsp; 更新时间：2009-4-11<br />
　　释尊入灭后，教团便出现了对“小小戒”的不同看法；随着连佛陀直系的弟子也都圆寂，教团更有了对戒律及教义上的异议；部派佛教的产生，使佛教理论趋向多样化，各部派都以自宗的主张为佛陀的亲传。于是，又结集各自的“三藏”经典。在后期部派的“三藏”中，就具有大乘思想的萌芽［1］。如“大天五事”等所表现的，佛与阿罗汉之间在境界上拉开了距离。大乘佛法的形式特征就是在佛与阿罗汉之间有一勤求佛道的修行者――菩萨。可以说：菩萨的出现，使佛教展开新的局面，进入了初期大乘佛教时代。这是大约在公元前半世纪左右。［2］<br />
　　随着大乘思想的兴起，大乘经典也开始流行于各地。较早出现的是般若思想，此一思想主要表现在《大般若经》中，因此，一般都认为《大般若经》是初期大乘的主要典籍。而相比之下，《大般若经》本身也有一个发展的过程，考其较早的原貌，应与部派佛教有相关联的。从经文的形式和内容来看，《金刚经》（全称《金刚般若波罗蜜经》）所表现的正是既有早期佛教经典的形式，又有大乘佛法的重要思想。所以，有人认为般若类最早出现的形式是略本，而《金刚经》是比略本更早出现的［3］。可见，《金刚经》是部派佛教向大乘佛教发展时期的重要典籍。<br />
　　首先，我们看《金刚经》的开头所表现的释尊的形象，就是早期佛教及原始经典所共同表现的那样，佛陀是现实生活中的一位老比丘，过着乞食的生活，日常生活都是自理，是非常朴素、毫无虚玄的。另外，参加听经的人数也不象其它大乘经典那样好几千或几万人，甚至是“无量无边”。经的开头那段便说明了这一点：<br />
　　佛在舍卫国o树给孤独园，与大比丘众，千二百五十人俱。尔时，世尊食时，著衣持钵，入舍卫大城乞食，于其城中，次第乞已，还至本处，饭食讫，收衣钵，洗足已，敷座而坐。［4］<br />
　　从这段经文中，可以看出，佛陀是一位与众比丘僧一样，过着和合清净的僧团生活，每天到了该吃饭的时间，佛就披上乞食吃饭时穿的袈裟（平时只搭五衣），手持钵盂，然后到城里去有次序地挨家行乞，乞够了饭之后，回到住的地方吃饭，然后又把袈裟和钵盂收起来，再洗完足、敷好座，就坐到那儿了。此时，若无弟子们的问法，通常佛陀就坐禅入定（也有无问自说，但这是很少的。）；或者，就对大家作种种教诲。这是世尊日常生活的一般规律。也是早期佛教僧团的生活方式。<br />
　　在早期佛教的经典中，也是用这种格式来记述世尊讲法之经过的。有的大乘经典，在佛陀将要说法的时候，总是现各种瑞相，以此表示大乘佛法的微妙殊胜。而《金刚经》的开头却完全没有说到什么示现神通、瑞相之类的东西，从形式上看，这说明了《金刚经》要比一般的大乘经典更接近于早期佛教经典。<br />
　　其次，《金刚经》是通过须菩提的提问，释尊的回答（或释尊设问，让须菩提回答，然后佛再解释）来完成的。这种层层递进的问答式的说法，据说十二分教中的“方广”就是这样的问答体，即是对一个问题步步深入地探究，讨论完一个问题后，又引发出下一个问题继续讨论，这样环环相扣展转相生，最终把问题讲得非常清楚。此经的体裁正是这样，而其它的般若经典就没有这么明显了。<br />
　　如此经所提出的第一个问题，须菩提问佛：“善男子、善女人，发阿耨多罗三藐三菩提心，云何应住？云何降伏其心？”等佛回答完了之后，须菩提接着又问第二个问题：“颇有众生，得闻如是言说章句，生实信否？”佛又解答了他的提问。而佛陀在解答问题的同时，自己又提出一些设问，如在回答第一个问题的时候，说完菩萨应该“灭度无量众生，而实无众生得灭度者”，马上又提出“何以故？”然后又解答所设问的问题。这样就会把问题越说越深入，越讨论就越明白。无著、世亲在解释此经说须菩提提出来的有二十七个问题［5］，而这些问题的解答都是从“性空”的角度来讨论的。“性空”说作为大乘思想的主要内容，其弘传的背景也与部派佛教有着密切的关联。<br />
　　部派佛教时期，各部派之间对“法”的理解有些出入。如有部、犊子部等都认为“法体恒有”，说一切法都是有自性的。有的部派讲人空，也讲法空，却又偏向极端。所以，这时出现“性空幻有”的般若思想，批破了“有”与“空”的两种极端的偏执，从而使佛教自部派时期进入了大乘时期。这又在《金刚经》中体现出来。比如“三十二相见如来否？”的问题及其解答：<br />
　　“须菩提，于意云何？可以三十二相观如来否？”须菩提言：“如是如是，以三十二相观如来。”佛言：“若以三十二相观如来者，转轮圣王则是如来。”须菩提白佛言：“世尊，如我解佛所说义，不应以三十二相观如来。”尔时，世尊而说偈言：“若以色见我，以音声求我。是人行邪道，不能见如来。”［6］<br />
　　至此，按说已经正面解决了这个问题。但是，在大乘“性空幻有”思想中并非就这么简单。所以，经中又特别指出下文的教示：<br />
　　须菩提，莫作是念：如来不以具足相故，得阿耨多罗三藐三菩提。须菩提，汝若作是念，发阿耨多罗三藐三菩提心者，说诸法断灭，莫作是念。何以故？发阿耨多罗三藐三菩提心者，于法不说断灭相。［7］<br />
　　从上述两段经文中可以知道，如来是不可以“三十二相”来观见的，但又不可执著这“不以三十二相来观见”。为什么呢？如果执“有”音声色相而见如来，就是行邪道，反而见不到如来了；如果执“无”具足相，则是断灭空，又落入了偏见。所以，在部派佛教时期所产生的一些观点，是受到初期大乘思想的批评的。而大乘的重要思想就是不执二边的“缘起性空”。如在经文的末后说：<br />
　　一切有为法，如梦幻泡影。如露亦如电，应作如是观。［8］<br />
　　都是说明了“性空幻有”的大乘佛法之正观。这种独特而深刻的大乘教法，似乎就是针对部派的某些偏执而说的。“性空幻有”的思想，到中期大乘时期，在龙树、提婆的大力弘扬下，得以空前的发展。但后期大乘思想的理论又比此经的理论要深入和复杂的多。<br />
　　最后，《金刚经》中极力赞叹诵经的功德，尤其是书写、受持、读诵此经的功德更为殊胜。如此经对持经的功德所描述的：<br />
　　须菩提，若有善男子、善女人，初日分以恒河沙等身布施，中日分复以恒河沙等身布施，后日分亦以恒河沙等身布施，如是无量百千万亿劫以身布施。若复有人，闻此经典，信心不逆，其福胜彼。何况书写、受持、读诵，为人解说。［9］<br />
　　从“书写”和“读诵”两项，可以看出当时此经已经有了文字形式的存在。早期佛教对佛弟子们的分工是：比丘们的任务是修清净梵行、讲经说法（即是法施），而在家信众就修五戒十善、供养比丘们的生活所需，如衣、食、卧具、医药等等（即是财施）。到了部派佛教时期，部分僧团开始接受钱财的布施，自己拥有一定的产业。所以，比起布施来，就更加重视对佛经的读诵、受持，认为布施恒河沙等身命还不如书写、受持、读诵此《金刚经》。<br />
　　另外，从在家信众的角度来看，他们的修行环境没有出家的比丘们那么清净安宁，而只能作布施（修五戒十善）的工作，但是，布施修福却只能得到生天的果报，而不能得到最终的解脱。经过部派佛教的某些僧团接受四时（衣、食、卧具、医药）供养以外的布施（钱物等），僧团有了自己的财产之后，无疑的在居士们的心目中的地位，就不如早期佛教时那么高了。这样，出家人拥有产业可以修行，当然就允许在家人也可以修行了。而且，法布施要比财布施的功德更大，一直是佛教的主张。所以，从自己的内心去修行的功德，就比布施修福的功德要大得多了。这种观念的微妙转换，开启了弘扬大乘佛法的思路。同时也体现出此经正处于初期大乘的阶段。<br />
　　自佛陀入灭后，由于弟子们对佛陀的无限怀念，而产生对佛陀遗物、遗迹的崇敬，赞叹拜塔的功德［10］（据说制多山部就是因拜制多〈即塔〉而命名的）。到了初期大乘时代，重视般若智慧和内心的修证，因此，在拜塔、供佛的基础上，指出了“读诵、书写”大乘经典的功德更为超胜。这点在其它的大乘经典中也有明显的表示。这说明早期佛教和部派佛教都较重视事相上的行持，而大乘佛教却更为重视理性上的证悟。如此经说的：<br />
　　在在处处，若有此经，一切世间，天、人、阿修罗，所应供养，当知此处，则为是塔，皆应恭敬，作礼围绕，以诸花香而散其处。［11］<br />
　　这里明显的可以看出，初期大乘的时候，对佛经的读诵受持已经可以等同于部派佛教时期兴起的对佛塔的崇拜，弟子们也从对佛陀的无限怀念中走出来，从而更理智地面对现实，这也是大乘佛教得以兴起的重要因素。<br />
　　以上所举是《金刚经》中的大乘思想，也是相通于所有大乘经中的大乘思想的。但是，后期大乘经典之所以与早期大乘经典有所区别，主要还表现在以下几个方面：<br />
　　第一，对“缘起性空”的表述：早期大乘对“缘起性空”的解释是相当朴素的，到了龙树菩萨等大乘学者出现的时候，对它的解释就变得非常复杂而显得面面俱到了，也就更具有逻辑性、具有更深入全面的理论结构。而《金刚经》所表达的“缘起性空”，恰好又是非常朴素的。如经中对“缘起假有”的解释就用了很朴实的语言说：一切有为之法，都是象梦、幻、泡、影一样，没有真实不变的永恒性等等。更接近于早期佛教以及部派佛教时期的经典。而在后期的大乘思想中却表现出了“缘起即空，非空故空，即幻有而性空；空即缘起，非有故有，即性空而幻有。”这与早期大乘的解释是不太相同的，也就是与《金刚经》中所表现的有一定的差别。对比之下，可以看出《金刚经》要比后期大乘的经典更早一些出现。<br />
　　第二，对“佛性”的表述：后期大乘经典，部类众多，内容繁杂。而其所阐扬的则是以如来藏、佛性、及与如来藏思想接近的佛菩提、涅等功德为一般倾向。尤其在对“佛性”的描述时，更显得深刻透彻而全面的。然而，早期大乘经典对“佛性”的阐扬却是朴素而原始的，只是说明了“佛性”是“即相而离相”的。不象后期大乘经典所描述的那样：“佛性是非空非有，竖穷三际，横遍十方”。从“空”又进入“不空”。以及说明了“佛性”具备十力无畏，十八不共法等等的功用和一切世出世间万事万物，甚至于十八空等等的含义。而在《金刚经》中所表现的正是简单而朴素的，如经中说的：“若见诸相非相，即见如来。”或者，“无我相、无人相、无众生相、无寿者相。”可见：早期大乘与后期大乘在对“佛性”的阐发上有所不同。从而，更加显示出《金刚经》所具备的这份朴素与原始。<br />
　　另外，对“佛身”的表述：早期佛教的时候，佛陀是僧团中的一员，不存在对“佛身”的不同解释。佛陀入灭之后，部派佛教开始，弟子们对“佛身”的理解逐渐产生变化，从“有相”过渡到“无相”。所以，早期大乘时期的“佛身”，是明了而简单地“无相”的。如《金刚经》的偈颂说：“若以色见我，以音声求我。是人行邪道，不能见如来。”这是“佛身观”从一位“老比丘”到“三身佛”的一个重要环节。后期大乘对“佛身”的描述是繁复而多样的，因佛的多种功德而给予多种的“佛身”表述。如“法身、报身、化身”等等的形成与阐述。但是，早期大乘相对来说就没有那么全面了，而在《金刚经》中所表示的，正是比较简单地说明了“佛身”是“无相”的。<br />
　　综上所述，《金刚经》中包含了许多早期佛教及部派佛教的信息，同时更着重大乘般若思想的阐发，又比后期大乘对般若思想以及佛性、佛身的叙述更为简单而朴素。对于如何处理日常生活问题、如何安顿自己的内心世界，站在大乘的立场，作了较明白的解说，大乘佛教的主要思想都已经包摄在内。从它的形式和经文的结构，以及所表达的内容上，都可以看出：此经既与早期佛教及部派佛教有着密切的关联，又是大乘佛法的主要思想和简单的理论结构。这也正是初期大乘佛教的特色。<br />
　　注释：<br />
　　［1］：部派佛教时期，提出了“心性本净”及“佛身无漏”、“一音说法，随类得解”等观念，但是，还没有系统化，所以说它是大乘思想的“萌芽”。<br />
　　［2］：初期大乘是指大乘思想的产生到龙树菩萨等的出现为止，约公元前50年至公元二世纪左右。<br />
　　［3］：参见吕j《印度佛学源流略讲》87页，上海人民出版社，1979年10月第一版。印顺法师说：“鸠摩罗什所译的《金刚般若波罗蜜经》、《菩萨藏经》、《富楼那问经》等，也都是‘初期大乘’的教典”（《印度佛教思想史》光碟版，84页）。<br />
　　［4］：《金刚般若波罗蜜经》鸠摩罗什译，《大正藏》卷8，748页下。<br />
　　［5］：参见吕j《印度佛学源流略讲》87页，上海人民出版社，1979年10月第一版。<br />
　　［6］：《金刚般若波罗蜜经》鸠摩罗什译，《大正藏》卷8，752页上。<br />
　　［7］：《金刚般若波罗蜜经》鸠摩罗什译，《大正藏》卷8，752页上。<br />
　　［8］：《金刚般若波罗蜜经》鸠摩罗什译，《大正藏》卷8，752页中。<br />
　　［9］：《金刚般若波罗蜜经》鸠摩罗什译，《大正藏》卷8，750页下。<br />
　　［10］：参见印顺《初期大乘佛教之起源与开展》43页，正闻出版社，民国七十一年九月再版。<br />
　　［11］：《金刚般若波罗蜜经》鸠摩罗什译，《大正藏》卷8，750页下。<br />
