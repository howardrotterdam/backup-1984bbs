																											<div id="postmessage_602337" class="t_msgfont"><a href="http://www.wyzxsx.com/Article/Class22/201005/149023.html" target="_blank">http://www.wyzxsx.com/Article/Class22/201005/149023.html</a><br />
<br />
吃饺子要吃饺子皮儿<br />
<br />
5月4日，北大112周年校庆。温家宝总理第三次亲临北大，与广大同学谈笑风生，校园里洋溢着节日般的气氛。我的学生陈灏庐有幸跟温总理握手并交谈，随后许多同学都抢着来握陈灏庐的手，沾点福气，以克妖孽。过不多时，灏庐接到姐姐的电话，母亲在医院里溘然去世了。灏庐一天之内，悲喜交加，匆匆登上了南下的列车，半夜给我发来短信，感叹人生何其无常。孔老师呆立窗前，默默无语。但听夜雨淅沥，间杂几声猫鸣。<br />
<br />
近期的日记涉及一些重要活动和他人隐私，就不在博客里流水公布了。只捡若干可以排忧解闷之事，晾晒于此吧。<br />
<br />
五一节带着老人，夜游北京，火树银花，笙歌处处。此情此境，颇怀疑那些忧国忧民的呐喊有什么用。设想中央领导考察民情，二环三环上转一转，这老百姓的日子难道不是“越来越好”吗？虽然吃的穿的都是毒品次品，但毕竟吃得挺香、穿得挺靓，谁能搞清饺子皮儿和饺子馅儿的区别啊。<br />
<br />
节前连续收到几个食品博览会的邀请，请我去免费品尝指导，交通住宿全部免费，还有礼物赠送。孔和尚动了馋心，但又不免狐疑，我也不是食品专家，只不过喜欢胡吃海塞，请我去根本谈不上任何指导，只能是白吃而已啊。还是俺家领导高明，眼睛虽然跟余则成一边大，但目光却比余则成还如炬，一语道破天机：“你就那么馋啊！人家请你去，是利用你的名气做广告，你以为是免费啊，其实人家省了几百万的广告费呢。那帮孙子你还看不透？你去吧，吃了那些有毒有害的食品，电视上一播，你就算代言人，将来出事儿，连你一块逮起来，我可不给你送饭，让你贪吃！万恶贪为首，这道理你不懂啊？”<br />
<br />
看来还是在家读书，安全、安心、安逸哟。速读了一堆新出版的小说散文，大约有20多斤。《文艺争鸣》2010年4月号上发表陈晓明《再论“当代文学评价”问题――回应肖鹰王彬彬的批评》。当代文学评论界近来发生了很多争论，在我看来这是好事。争论中难免有意气用事和宗派立场，但只要不是依仗权势，双方公平辩驳，道理总是能够讲清的。当然，讲清了不等于听清了，听清了也不等于看清了。<br />
<br />
5月2日读了杨支柱《“叶廷芳提案”与中国的计划生育政策的走向》。文章指出，强制计划生育导致政府权力侵入家庭这个个人权利的最后堡垒，并且深入到家庭中最隐秘的性和生育领域，即使排除暴力执法，“见证怀孕，持证生育”也严重侵蚀个人的人格尊严和隐私。把生育权利变成“不得生育”或“不得再生育”的义务，则混淆人们的权利和义务观念，而一个连权利和义务都分不清的民众所组成的国家怎么可能谈得上法治？易富贤以规范的社会科学研究跟官方人口学家对垒。易富贤雄辩地指出：中国现在根本没有13亿人口，国家计划生育委员会和支持他们的人口学家动辄修改人口数据，目前中国的总和生育率实际上只有1.2左右而不是国家计划生育委员会所说的1.8，全国每年只增加人口约400万左右而不是国家计生委所说800-1000万，而今后随着老年人口的堆积每年死亡人口数将急剧增加，以目前的生育意愿，要是不停止计划生育的话，中国人口不可能增加到13亿（然后快速减少），而不是国家人口发展战略组宣传的15亿。近几年的人口增长情况都与易富贤的预测接近而与国家计生委的预测相距甚远。通过详细比较世界各国的自然资源，发现中国的人口过多只是一个流传很广的谎言。通过与印度等国比较，发现计划生育并没有为中国社会、经济发展作出任何贡献，相反计划生育引起的家庭消费结构畸形是中国经济问题（低工资现象、就业压力等）的根源。易富贤并认为马寅初人口论是错误的，要是依照马寅初人口论，从1959年1979年少生3亿（实际上4.57亿），意味着1959年以后出生的人口的近2/3是不能出生的，现在中国可能已经因为老年化而崩溃，已经沦落为一个毫无生气、丧失可持续发展能力的国家。<br />
<br />
谈到人口问题，想起年初，郁保四之弟郁保五在东莞的小厂倒闭，便来京闯荡。哥俩商议后，欲开一足道馆，郁保四请孔和尚给取个别开生面的好名字。孔和尚说取名费5千，看在老朋友，3千。保四应允。孔和尚沉思片刻曰：“有了，就叫昆仑三圣。”保四大喜，连称好名字，好名字。遂予孔和尚3张优惠券，曰每张可抵千元消费。五一节足道馆开张，孔和尚持券前往，见保四、保五正双双泡脚，痴对电视看韩剧焉。老孔疑惑曰：“听说足道馆都有小姐给捏脚，奈何二位自己泡脚耶？”保四曰：“自己泡脚1千，泡完捏脚2千，若要小姐捏脚，则3千。吾兄弟以身作则，实行低碳泡脚，节省开支也。”孔和尚曰：“尔兄弟二人无乃太黑矣！也罢，反正这3张优惠券无处可用，今日就奢侈一回，唤个小姐捏脚吧。”保五曰：“如今民工荒，北京尤其是老年社会，小姐尚未招到也。小弟亲自操刀，可乎？”老孔曰：“你五大三粗，手如钢筋，捏坏了俺的纤纤玉足咋整？你捏只给1千。”保四曰：“吾兄弟二人联手，胜过小姐必矣，仍只算你3千。”孔和尚曰：“不行，你二人联手，只算2千。”保四回头唤曰：“王哥，出来哉！看来只有三捏一，他才肯付3千矣。”老孔仰天叹曰：“名不虚传，真乃昆仑三圣也。”<br />
<br />
有朋友问，孔老师经常调侃郁保四和王青皮，你真有这么两个同学吗？抑或另有深意？军机处转来一位“不亦说乎”的解读，可以参考。“一方面，孔老师在多篇博文创作中放弃了单个人作为统一主体的书写幻觉，转而将某单一主体分解为以孔、王、郁三个不同人称为代表的主体碎片。《师姐师妹何足论？》中的第一人称‘王’和第二人称‘郁’实为一体，后者乃前者的投射或精神的异化。第三人称‘孔’则又是对第一人称‘王’的静观和思考，也是最终的准理性回归。在这漫长的对白中，你是我讲述的对象，一个倾听我的我自己，你不过是我的影子。都还太年轻，尤其是那个老王。我、你、他（孔、王、郁）的不同表述形式实际上就是当前文化语境中主体碎裂命题在文学中的映照。另一方面，碎裂后的主体恰恰形成了不同的视角和观察方式，为文学表达提供了新的空间。作者可以通过主体分裂后的不同的视角来反观主体自身，从而解决了单一外视角的统一摹写可能带来的简单片面化问题，也获得了某种突破当前语言困境的表述手段。”<br />
<br />
5月3日，军机处发来敌情简报。汉奸走狗最近调整了打击孔和尚的策略。以前是疯狂咒骂、一笔抹杀。现在看到这招不灵，孔和尚拥有千百万有见识有水平的拥趸，不上他们的当。于是近来采取摇头叹息之术。或是称赞孔和尚以前的文章写得好，现在江郎才尽了，令人失望。或是诡称孔和尚某些文章写得好，而另外一些文章不堪入目。总之是挑拨孔和尚情趣各异的粉丝之间的矛盾、孔和尚与各类粉丝的矛盾、粉丝自己跟自己的矛盾，最后是孔和尚自己跟自己的矛盾。手法似乎很巧妙，但少剑波参谋长说得好：“狐狸再狡猾，也斗不过好猎手啊！”只要汉奸们是昧着良心说话，是颠倒黑白说话，那就无论采取什么阴谋诡计，都会被戳穿，都会被鄙视，都会加重自己的罪孽，害人不成，却先把自己抛进了历史的垃圾堆。<br />
<br />
5月3日，读《国外理论动态》2010年第4期。美国莫里斯写的《蒙古人民革命党的转变》一文很有参考价值。放弃革命理想，实际上就是放弃了大多数人民的幸福。联想到侯艾君发表在《南风窗》2008年第16期上的《“泛蒙古主义”幽灵：为何日本与达赖最感兴趣》，文章指出，一种前景令人担忧：西藏、新疆甚至内蒙古同时爆发类似骚乱，或各种潜伏的危机同时爆发，形成某种“联动”、“同步化”。内蒙古的佛教寺院向来与青海、西藏在教派和人员方面联系紧密；此外，一些破坏性的外部因素不容低估。多年来，许多外国组织和个人深入辽远的中国内地，其中一些人负有某种秘密使命：收集情报，赢得当地居民的好感，收买人心，挑动特定族群间的对立，制造事端。这些手法并无新意，但却会产生效果。类似前景在考验我们的政治意志和智慧。设想呼和浩特、包头、赤峰都成为美军基地之后，北京上海还搞什么世博会鸡脖会？大约只能搞搞“前中国专制恐怖图片展”吧。这个世界上最恐怖的基地组织，不用东找西找，就在白宫和五角大楼也。<br />
<br />
今天是马克思生日。中午母亲包了猪肉白菜饺子，孔和尚吃得很开心。赐给小赵半只，那厮吃了馅儿，丢下饺子皮儿跑了。母亲说，不吃饺子皮儿算啥吃饺子？我说，大概饺子皮儿有农药吧。孔和尚便看胡锦涛的传记，从胡锦涛同志的成长，看出了毛泽东时代的伟大。<br />
<br />
有人（是不是人不知道）说毛泽东时代，不是工农兵的子弟就不准上学，至少不准上大学，更不准入党入团。让我们看看胡锦涛同志的学习生涯吧。<br />
<br />
胡锦涛出身于商人世家，他5岁上了五巷小学，后来转到大浦小学。1985年，大浦小学五（三）中队举行“老校友回来了”活动，时任团中央第一书记的胡锦涛致信曰：“我仿佛又回到无忧无虑的童年时代，那时我也像你们一样，胸前飘扬着红领巾，和小伙伴们一起过队日，为了解出一道算术题争论得面红耳赤，大概也会因为淘气惹老师生过气。而当我到了不再淘气的今天，才更加体会到，学校的生活有多么珍贵，学校里学到的知识，在今天的工作中有多么重要。”看，胡锦涛明明白白地说，毛泽东时代的小学生，是无忧无虑的，是又红又专的，当然，也是淘气的。又红又专加淘气，人生才是幸福滴！现在的孩子，不红不专，连淘气也不会，这过的还算是人的日子吗？<br />
<br />
1953年，11岁的胡锦涛小学毕业，进入私立泰州中学（简称“私泰中”，现泰州二中）读初中。请注意，那时候胡锦涛不但可以上学，而且上的还是私立中学，私立中学同样也受共产党的领导。初三时，胡锦涛同学因为品学兼优，加入了共青团，当时班上40多人，入团的不到10人。胡锦涛的同学夏道球回忆说，胡锦涛初中三年，品行一直是“甲等”。胡锦涛不仅学习好，还多才多艺。他创作了宣传党的政策的相声，跟同学们上街表演，很受欢迎，显示出过人的才华。共产党压抑了这个商人子弟的才华了吗？某些瘪三宣称，会说相声的人必然下贱，这些自以为喝咖啡就不是土鳖的、一贯溜须拍马的瘪三，看到这个材料后，不敢再说了吧。<br />
<br />
1956年，胡锦涛初中毕业，考上了泰州最牛的中学――江苏省泰州中学（简称“省泰中”）读高中，那时读高中，相当于今天读大学。胡锦涛门门功课都90分以上，但还不算班上成绩最拔尖的，因为泰州中学太牛了，院士就出了好几个。<br />
<br />
再后来就不用说了，胡锦涛考上了清华。他在清华写的文章，孔老师以前在博客里贴过。毛主席时代的清华大学，是多么朝气蓬勃、多么奋发有为啊。今天社会各界纷纷称赞清华大学培养出了很多科技人才和政治人才，但却忽略了一点，这些著名的人才，全部是毛主席时代的清华大学培养的。那时候看人，的确要看出身，你如果是地主土匪狗汉奸的后代，我们当然要看看，这有什么错吗？你们现在不也要看看我们家里有没有钱吗？但是那时候并非用出身把人看死，叫做“有成分论，不惟成分论，重在表现”。只要个人表现好，出身就不是问题。工农兵子弟，犯法照样惩治，学习不好也不能升学。达官贵人犯了大罪，也一样枪毙。当然，那时候的干部多数是有廉耻的，用不着枪毙判刑。这就是孔夫子说的“刑不上大夫”的本意。<br />
<br />
今天的干部咱就甭提了，胡锦涛总书记专门讲了“八荣八耻”，诚心诚意挽救他们。可是他们根本不往心里去，照样敷衍欺瞒。孔老师前几年到某地去为机关干部讲座，看见市政府大楼上高悬标语：“全体党员干部，一定要做到八荣八耻！”孔老师顿时又气又乐，对接待我的副市长说：“老兄，你们脑袋里装的是豆腐渣呀？那八荣是可以做的，那八耻，他妈的怎么能做呢？还嫌自己不够无耻啊？不是我骂你们，你们这不是拿胡总书记的教导当猴耍吗？中央知道了，还不收拾死你们？”<br />
<br />
那副市长居然想了半天才明白：“哦哦，搞错了，八荣是让我们做的，八耻是不让我们做的。”我心想，胡锦涛的胸怀得多么大呀？领导着这么个废物成堆的干部队伍，即使他们不腐败、不卖国，光凭这份智商，也足以把胡哥气死了。<br />
<br />
然而今天想想，那八荣八耻，究竟算饺子皮儿还是饺子馅儿呢？</div>

							
							
							
							
