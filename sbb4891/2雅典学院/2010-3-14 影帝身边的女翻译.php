													<h2>招捧t嘏郎</h2>
						
						
																											<div id="postmessage_518636" class="t_msgfont">温家宝记者会女翻译现场流利译出古诗词受热捧<a href="http://www.sina.com.cn" target="_blank">http://www.sina.com.cn</a>&nbsp;&nbsp;2010年03月16日05:10&nbsp;&nbsp;北京晨报 <br />
<br />
女翻译张璐。<br />
<br />
<br />
　　北京晨报3月16日报道 人大闭幕后，温家宝总理的记者招待会成为中外媒体关注的焦点，温总理再次以睿智和博学、以富有文采的回答，迷倒了中外记者。与此同时，坐在温总理身边的美女翻译张璐，由于现场流利地翻译温总理引用的古诗词，也受到亿万观众和网民的热捧。昨天，记者在各网站微博(<a href="http://t.sina.com.cn" target="_blank">http://t.sina.com.cn</a>)上看到，张璐的排名超过了刘翔获得世锦赛第七的消息。<br />
<br />
　　“我觉得她翻译得很好，的确是国家级水准。”清华大学外语系主任罗立胜在接受记者采访时表示，从专业的角度来看，整个发布会翻译很流畅，不少政治领域的词汇，译得都比较恰当。发布会上温总理引用的古典诗词，由于中文和英文的文化背景差别很大，其中深刻的文化内涵很难在翻译中体现出来，一般都只能翻译大意。不过她表现出色，这些诗词总体上都译得很到位。<br />
<br />
　　网友们的热评则更富有感情色彩，大家议论最多的，正是她翻译古典文学的能力。温总理引用《离骚》诗句“亦余心之所善兮，虽九死其犹未悔”，许多网友发现张璐的翻译有很巧妙的地方，“汉语习惯说‘九死’，英语一般要死上thousand times(一千次)才够。”认为张璐把“九”翻译成 “thousand times”非常贴切，猜测她是出身名门，饱读诗书。还有不少网友为张璐的气质倾倒，KelseyLiu在微博中写道：“高翻张璐就是我永远的榜样啦！她才是真正的职业范儿，有气质，有品位，发型也好看。我要向她这个目标勇往直前！”<br />
<br />
　　记者了解到，张璐是外交学院国际法系1996级学生，2000年毕业，后来改行做高级翻译，现任外交部翻译室英文处副处长，有着丰富的高翻经验，是国家领导人的首席翻译，也是重要对外场合的首席翻译之一，外界对她的赞赏是：“反应敏捷、举止优雅。”<br />
<br />
　　另据媒体报道，由于温总理的语言非常丰富，谈话时常常引用古诗词和专业词汇，需要翻译做大量前期准备工作，一般多会在小本子整理出总理以前说过的古诗句和成语，免得临场慌乱。另据张璐外交学院的小师妹介绍，张璐非常聪颖，也很勤奋，常常加班到凌晨一两点，几乎每天听BBC、VOA、CNN，喜欢做笔记，爱看《参考消息》和《环球时报》等。<br />
<br />
　　-现场翻译：<br />
<br />
　　1、 “亦余心之所善兮，虽九死其犹未悔。”张璐译文：For the ideal that I hold dear to my heart，I’d not regret a thousand times to die。<br />
<br />
　　2、“人或加讪，心无疵兮。”张璐译文：My conscience stays untainted in spite of rumors and slanders from the outside。<br />
<br />
　　-工作历程：<br />
<br />
　　●2009年2月，温家宝访问英国会见首相布朗，张璐担任现场翻译。<br />
<br />
