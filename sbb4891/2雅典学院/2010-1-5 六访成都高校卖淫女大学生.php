　　“我知道，可是。”<br />
<br />
　　“可是什么。。。”<br />
<br />
　　“你做了多久？”我问一个我第一次见的小姐。<br />
<br />
　　“从上高中开始，已经两年了。”<br />
<br />
　　“你还在读高中？”<br />
<br />
　　“对，礼仪职中。”她还是那种职业一笑“吃饭吧，我感觉好恶心”<br />
<br />
　　“没什么的，我早就发现我心里变态了。你知道为什么吗？帅哥？”<br />
<br />
　　“为什么？我怎么可能知道”<br />
<br />
　　“因为我的那里有一颗痔，我见了男人就想勾引，没办法，天生＊命啊，我无所谓了”<br />
<br />
　　“你真的很＊。”<br />
<br />
　　“你才高中啊，没有搞错？！你家里知道吗？”<br />
<br />
　　“我爸爸妈妈早死了！”她很愤恨，我知道她的父母早就不管她了。<br />
<br />
　　“你是姐姐的朋友也就是我们的朋友对吗？我知道只有你还把我们当人看。”<br />
<br />
　　“你今天晚上有空吗，我今天不出台。”<br />
<br />
　　“。。。”<br />
<br />
　　顺心餐吃饭的人渐渐多了起了，我们起身离席，席间一个川师的先走了，西博会一个客人把她要走了，我们几个也进了留言水吧。<br />
<br />
　　我发现我和他们在一起我也差点变坏了的，不过我还始终把持自己的道德底线，我一直在很礼貌的拒绝着她们的接近，一会我的女友过来了。她虽然很生气但是还是没有发火，我知道她是再给这些女人面子而已，要不是女友一只提醒我我担心生理本能就让我犯错误了，虽然最后女友还是离开了我，但在写这个稿子时，我心里想的还是她。。。<br />
<br />
　　留言水吧没有南门那些喧闹，虽然我只是为了调查去过几次而已，我知道在留言还是很难产生这种最原始的性的交易，也不足以让我对这里的女人产生原始的冲动，或者我对她们根本不会产生冲动，因为我记着她们说得“我们是小姐”。我不知道这次阉割之后没有了我们的思想的稿子会不会被转移，不过只要能起到教化的目的我们还可能继续。一共15小节，现在发的是第6节阉割版这还是我们的调查手记，我们只会在里面加一点保护性的内容，全程展示我们的调查内容，和调查中发现的社会问题。请大家不要再往外网发，不然我可能会修改某些真实的内容。<br />
<br />
　　美丽的光华园－－调查手记之七说实在的，我是很喜欢西南财经大学的。我大一在九里提的时候就很喜欢去才大看美女，事实上财大也是有美女的，我们经常跑到她们学校洗澡堂旁边的小楼去看，看这些出水的美女。我们也喜欢再夏夜漫步在西财，看着一对对的美女组合。可是这次我去西材的心情是沉重的，我不知道是不是我知道了太多的这些女人的所作所为。我开始不敢看美女，我希望美丽的光华园还是我心中的一片净土，因为我很早就去过川师很早就对川师的女生失望，所以我不敢去，不敢去破坏西财，破坏我心中的这片净土。<br />
<br />
　　但是，该离去的始终要离去，西财给我的美丽的印象就在这一个月毁灭，或者着对我们这些喜欢财大喜欢光华园的人是种煎熬。在写这篇手记的时候我也算是在自虐。<br />
<br />
　　初夏的光华园是美丽的，夜里的光华园是宁静的，来到财大我突然想起了一句话“世界上最脏的是金钱”。也是啊，财经大学，中国人民银行直属高校。这片不却钱的学校竟然也却钱。我在心里笑着。。。<br />
<br />
　　小a很早到了光华园，我们约在那家大盘鸡店，因为她们知道我是西北人。<br />
<br />
　　今天的小a很漂亮的，穿一件粉红色的上衣，再夜色里多了几分柔美，今天或者更多的是她姐妹之间的交锋。<br />
<br />
　　“梅，该收手了吧？”<br />
<br />
　　“收手？不可能，我爱钱，我却钱。”<br />
<br />
　　“现在你已经挣够了你当年承诺的2万啊，你的大学生活可以过得很好了啊？”<br />
<br />
　　“我不知道，反正我是无法再收手，可能是我对男人的一种报复吧。”<br />
<br />
　　“梅，不要这样好吗？我们姐妹一场，我不能看着你继续这样下去。”<br />
<br />
　　“a，别劝我了，我知道你对我好，可是我已经无法收手，我要报复！”<br />
<br />
　　“我知道你恨男人，可是你的病再这样下去肯定要加重的啊。难道你不怕？！”<br />
<br />
　　“无所谓了，他们传染给了我，我再传染给他们，一报还一报啊。”<br />
<br />
　　“梅，你可不可以不在出去，你的条件这么好，你既然不愿出来，你可不可以多吸引一点回头客，这样你的身体可能不会染太多的病。或者你找个男人把你包了算了。要不你只接大学生，可能他们还干净一点？”<br />
<br />
　　“我知道你对我好，可你知道我们这一行，既然开始就没有结束，要不是gg不听的劝你你也不会收手啊。我真的很难，我怕我妈妈有一天会知道，只有她还爱我，我不能让她伤心”<br />
<br />
　　“那你就收手。”＊＊了一句。<br />
<br />
　　“你能救得了小a姐，可是你救不了我。你能对我象她那么好我也就出来了。我不怕mama她们的，我是她们的王牌。”<br />
<br />
　　“你可以出来啊，会有人关心你的。或者你没钱了再说现在还有2万的嘛。”<br />
<br />
　　“哎，你不会懂得，我也后悔啊，我全完了，得了滴虫，毁了自己，两万不足以补偿我！<br />
<br />
　　”<br />
<br />
　　“我们女人没有其他的只有身体，我知道那些男人是爱我的身体而不是爱我。所以我还要报复”<br />
<br />
　　“。。。”<br />
<br />
　　“你们学校的其它几个姐妹呢？千万要注意身体啊，要不行我们学校男生可多着呢哈，你去聊天室晃吧，总比外面得人干净，你说呢？”<br />
<br />
　　“你们学校的我有不是没接过，不过他们没钱。但是我还是喜欢和他们做的，毕竟是一帮才子啊。”<br />
<br />
　　我的思绪很乱，很难听她说下去，我只是在想我大一曾经遇到过的财大美女，那么单纯，那么可爱。可是世界再不经意间还是变了，变得那么陌生。那一夜我没回来，我们在行政学院玩到天亮。我的心还是那么乱。<br />
<br />
　　＜走进＞调查组第二次申明，我们昨天发的只是我们手记的三分之一，我们在后面合适的时间，我们会把修改过的保持社会本来面貌的调查手记再次发布。不过调查报告我们不会发布，我们争取以内参的形式在领导层参考。后面的才是我们调查的核心内容，一些内幕的东西我们现在看来已经不便披露了！！不过我们回发表我们酒吧遇险怎么在小姐的帮助下离开等比较注重人性的东西的。我们出与自己和对那些主人公的保护已经将我们和她们的联系部分掐断，暂时避避风头。我们已经在公网发现了我们的调查，我们表示遗憾，我们暑期调查将集中在一个农村问题上，前期调查已经进行了一年，现在正在学习社科院的三农问题专著。以便我们的调查分析更有意义。再适当的时候我们欢迎大家指点。我们是大三的，我们还有激情和力量，希望学弟们再以后类似的活动中注意保护自己。谢谢学校，谢谢同学们，谢谢一切正义的力量！这篇详尽的材料仅仅只是所有调查报告的1／3，还有2／3不便披露。目前，这方面的情况已经引起了中央政府高层关注，以及全国主流媒体的广泛重视。<br />
<br />
　　面对这一幕幕催人泪下的学院秘闻面对这些被金钱物欲和西方思想彻底洗脑的年青大学生，我们的价值观真的迷失了吗？传统道德和当代夹杂东西方古今各种气息的思潮谁对谁错？是否精神文明建设在现在变成了空洞乏力令人嗤鼻的口号？大学阶段是人一生中树立正确的辩证唯物世界观，人生观的最重要的阶段，在追究这一现象外因的同时，我们的高校思想道德建设是否应该改一改了？！<br />
<br />
　　＜最后，感谢这几位大学生采写的这篇报告，它可能会挽救不少人不幸的人生＞<br />
