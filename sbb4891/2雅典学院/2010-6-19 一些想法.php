													<h2>一些想法</h2>
						
						
																											<div id="postmessage_692128" class="t_msgfont">因为网站打不开才知道了GFW，然后了解了这些敏感的东西，快有一个月了。在推特、1984bbs、博讯网等等也看到了一部分东西。有不少困惑的地方。<br />
我觉得大家讨论的东西有相当一部分东西是围绕着六四，还有一部分是围绕着贪污人治，还有一部分是GFW。<br />
我不知道过上十年甚至不到十年国家会不会重新评判六四？下一届领导人是更年轻的一代，思想上也应该大有不同。六四早晚会有正面的评价。只是时间问题。<br />
贪污人治，我觉得这才是我们国家最应该解决的问题。我来自一个小城镇，老百姓对于官员贪污对于违法行为都习以为常，很愚昧麻木。我想中国大多数的小城镇大概都这样吧。<br />
GFW，我也挺不爽的，但是网民发100块钱，估计没几个人会再抱怨GFW了。。。因为这些网站对于我们很多人来说都没啥用。<br />
很多人讲民主讲人权，我觉得这是针对每个人的利益出发，而那些真假五毛常常说国家发展的快，是针对国家出发。这就导致了各大论坛的论战。出发点不一样。但是这其实并不矛盾啊。<br />
对于美国阴谋论，我不知道有没有阴谋，要想搞清楚这个问题，起码得是政治历史经济方面的专家吧。。很多人说的话都挺片面的，提供的资料也有假的一面。但是美国攻打伊拉克应该是为了石油吧。<br />
1984上有些人完全是脑子有问题，发了很多白痴的帖子只知道骂人五毛却给不出理由，这种人完全就是2b。<br />
我觉得很多大谈民主什么的，只不是来过口瘾罢了。他们没有为国家的改进做过任何贡献却大肆攻击那些真假五毛。<br />
我现在还在读大学，以后有了一定的收入，我会多参加一些类似为“福建网民案”伸冤的活动，因为这样是符合实际的能够做到的行动而不是口头说说就想解决问题。<br />
<br />
说错话了。<br />
