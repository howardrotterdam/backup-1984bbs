<br />
<strong>三. 史吉特法则（Skitt’s Law）</strong><br />
<br />
Skitt’s Law是墨菲定律（Muphy’s Law）的网络版，注意，不要与Murphy’slaw混淆。Muphry’s law是一句流传甚广的格言：<br />
<br />
<br />
假如你写任何关于批评编辑或校对的文章，那么你的文章中必定会存在某种形式的错误。<br />
<br />
后来在网络上衍变成了Skitt’s Law，意思是：<br />
<br />
<strong>任何试图指责纠正另一个帖子拼写或语法错误的帖子，本身必定含有拼写或语法错误。</strong><br />
<br />
这法则的话外音就是说：假如你要在bbs或blog等发帖，纠正别人帖子里的拼写或语法错误，一定要注意把自己的发言检查3篇！<br />
<br />
为什么会有此法则？初次听起来还有点荒谬，可是网络世界里却不断在上演。既然别人的帖子你读懂了，错字你也明白了，还有什么能让你愤怒，以致费时去批评别人拼写和语法？甚至人品？<br />
<br />
当一个人混乱或忙于骂战别人拼写或语法时，他往往把注意力集中在对方身上挖苦，此时，自己的拼写语法通常都抛至脑后，错误自然难免，最后造成比别人更为尴尬窘迫的下场。<br />
<br />
要理解，这个法则不是阻止你纠错别字，而是提醒，心态很重要。<br />
<br />
<strong>中文网路有句话：傻逼从来都是自证的。当你因某人的个别错字莫名兴奋时，谨记此法则。</strong><br />
<br />
<br />
四. 第34条定律（Rule 34）<br />
<br />
（警告：一旦你熟悉Rule 34，整个人不仅完全认同Rule 34，并且看待事物的方式，人生观，都将产生变化，谨慎深入）<br />
<br />
Rule 34是Rules of the Internet的第34条，是所有rules当中，唯一一条，几乎所有人都认同的法则。<br />
<br />
Rule 34的定义是：<br />
<br />
<strong>“If it exists, there is porn of it. No exceptions.” (如果它存在，一定可以色情化。]有例外。)</strong><br />
<br />
就是这么条简单的定律，在互联网时代，它成立了。以前仅靠软文学，黄色笑话，AV等媒介，尚不足以印证此法则，而在互联网时代，一切你能想象的事物，都可以用图来使之色情化，毫无例外！<br />
<br />
此法则的精髓在于毫无例外，因为当你试图质疑某个东西不能色情化的时候，你脑海中大概已经在酝酿着那东西的色情化了，此时的状态就是rule 35，”当你发现某个东西没有色情化时，它正在创造中”。<br />
<br />
如果你没理解，可以反面想想：一个质疑拖鞋不能色情化的家伙，他得想过，看过多少色情化作品才会出此质疑？<br />
<br />
中文网路里有句话精辟的阐述了Rule 34的状态：<strong>湿人眼中什么都是湿的，如有一颗淫荡的心，那么全世界都将为之淫荡。</strong><br />
<br />
当然，你在和谐世界里是不会发现rule 34的威力的。据我观察，中文网路里，rule 34的主要影响在文字方面，无数的荤笑话里边。<br />
<br />
rule 34不能再细说了，这条法则完全展示出来对某些人是恐怖性的，原因如rule 28所说：There will always be more fucked up shit than what you just saw.<br />
<br />
<br />
<strong>五.关于门户的定律</strong><br />
许多人上网初期最先接触到的就是门户网站，而且很可能你会一直浏览某个门户网站，不过在我看来，门户并不适合挑剔的网民。第一原因是”国内门户未被赋予新闻采访权”，因此基本属于贩售组织消息，除了专题的制作外；其二是糟糕的网站体验，相信你不会每次都在满屏的文字当中，选到感兴趣的新闻。最后一个原因就是众所周知的了。<br />
<br />
关于门户还可看下土摩托写的三大定律：<br />
<br />
<strong>一，三大门户网站都是流氓网站。<br />
<br />
二，任何事情，国内的记者参与得越多，报道效果就越糟糕，因为三大门户总是会把所有报道中最差劲，最弱智的报道放到首页。<br />
<br />
三，门户网站看多了，你一定会变成一个流氓。</strong><br />
<br />
以上偏激吗？如果你知道软文，推广，公关稿在门户是难以区分的，那么你自然会避而远之。另外还有位推友是这么总结的：<br />
<br />
@tuidaogfw: 腾讯=太监+间谍 新浪=双面人 内外双重标准 凤凰=和事老 谁都不得罪爱打擦边球 网易=不食人间烟火 有一种追求真理的冲动 新华网=婊子 一副我是流氓我怕谁的无赖相<br />
<br />
这就是门户的一些现状。当手里带一块表时，这就是准确的时间，而带着三块表的人，要用哪块表来跟别人对时间？<br />
<br />
<strong>六.关于论坛的定律<br />
论坛里，你我各有一个苹果，交换后还是各一个苹果；你我各有一种看法，交换后只有一种看法，对方傻逼。</strong><br />
<br />
<br />
每人心态都不同，有的带着战斗的激情，有的来娱乐，有的互比”苹果”：老子的苹果又大又红又专，老子手中的苹果砸过牛顿的头！<br />
<br />
Danth’s Law这么描述：“<strong>假如你一味坚信你能在一场网络争论中胜利，很可能你会输得非常惨</strong>”。这就像通常说的，认真你就输了！在鱼目混杂的论坛里，一不小心就陷入troll里，莫名的来气。<br />
<br />
因此，学会对话，识别坑，就要理解这流传已久的BBS十大定律：<br />
<br />
第一定律：回复最多的帖子，是拥有马甲最多的人写的。<br />
<br />
第二定律：帖子的价值和回复的人数成反比；与感叹号的多少成反比。<br />
<br />
第三定律：网名的香艳煽情程度和该人的丑陋程度成正比 。<br />
<br />
第四定律：在BBS上好不容易成名了，可又厌倦了。<br />
<br />
<strong>第五定律：你拍过某人的一次板砖，以后就再也不会忘记他了。<br />
<br />
第六定律：有人爱现实所以爱网络，有人因为恨现实才爱网络。</strong><br />
<br />
第七定律：争论中没有获胜的，与女性争论，没有不惨败的。<br />
<br />
<strong>第八定律：拍你砖的人比捧你场的人更理解你的帖子。<br />
<br />
第九定律：高手对垒，一开始比的是才气和知识，最终分高下的是人格因素。</strong><br />
<br />
第十定律：签名档越复杂，头脑越简单。<br />
<br />
在论坛里要真正的实现平等有效交流，是很麻烦的事，于是有网友引入哈维尔等人在布拉格成立<strong>”公民论坛”制定的8条《对话守则》</strong>以期实现交流的效率：<br />
<br />
<strong>1、对话的目的是寻求真理，不是为了斗争。<br />
<br />
2、不做人身攻击。<br />
<br />
3、保持主题。<br />
<br />
4、辩论时要用证据。<br />
<br />
5、不要坚持错误不改。<br />
<br />
6、要分清对话与只准自己讲话的区别。<br />
<br />
7、对话要有记录。<br />
<br />
8、尽量理解对方。</strong><br />
<br />
由于中国特色的国情，很多不是本着交流，寻求真理的目的，而是来搅局，炫耀，抨击，奚落，他们分不清情绪与话语，于是网民又增加了第9条：遇到傻逼，还是要骂。<br />
七.关于网络群体的定律<br />
互联网就是个体与个体，个体与群体，群体与群体之间的事，那么现实的一些群体规律同样适用。<br />
<br />
<br />
协同过滤：<br />
<br />
网络时代，爆炸性的信息，我们不得不利用协同过滤来筛选信息，只订阅符合自己口味的博客，只混对味的论坛，只看Digg来的内容。而所作的这一切，都是使浏览到的信息更符合自己，其中一个负面就慢慢显现，过滤掉了所有反对的声音。<br />
<br />
许多人是从完全安排好的电视报纸模式，突然转变到全自由选择信息的时代，很茫然，或很极端。<br />
<br />
我想说的是，让一些随机化的信息能出现在阅读器，留给刺耳的声音点时间。<br />
<br />
群体极化现象：<br />
<br />
群体极化是指：群体讨论时，众人的观点并不总是均衡了，而是极化了，使大伙最初的偏见得以加强，最后变得更为极端。<br />
<br />
互联网的出现使得更易成为极端主义的温床。 这在国外非常常见，一些典型的就是极端原教旨环保主义者。<br />
<br />
当然，极端未必不是好事，如同恶搞，人受自身思维的限制，有时只有把平常的事件夸张化，极端化，人们才会发现问题所在和反思。但必须要有这个意识：有时团体决策不见得比个人好，在群体极化现象下可能更差。<br />
八.微博定律<br />
这是推友总结的两条定律：<br />
<br />
<br />
微博第一定律：我关注你，未必指望你关注我；你关注我，也不能强求我关注你。<br />
<br />
微博第二定律：你我均有权保持沉默或话痨 也都有取消对彼此关注的权利且无需事先获得任意一方批准。<br />
<br />
其实以上可归结为“不要对我有幻想 “（via aiww）。<br />
<br />
永远不要意淫别人应当如何发推，一但进入对某人的幻想，他发出不符你妄想的推时，大脑程序会自动进入混乱机制。轻者纠结，不满自己；重者叫妄想的破灭，反咬一口。<br />
<br />
