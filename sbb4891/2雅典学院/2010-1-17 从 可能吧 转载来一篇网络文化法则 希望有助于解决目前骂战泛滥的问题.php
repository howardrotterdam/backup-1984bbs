																											<div id="postmessage_437092" class="t_msgfont">一个简单的问题：你会对话吗？<br />
发布:烟雨渡客 | 分类:网络杂谈 | 浏览:29 | 引用:0 | 评论:暂时没有评论<br />
你一个简单的问题：你会对话吗？<br />
<br />
以下《对话守则》摘自苹果日报的《苹论》：<br />
<br />
1989年，捷克知识分子哈维尔等人，在布拉格成立了“公民论坛”，制定8条《对话守则》，在街头巷尾张贴，内容是：<br />
<br />
1、对话的目的是寻求真理，不是为了斗争。<br />
2、不做人身攻击。<br />
3、保持主题。<br />
4、辩论时要用证据。<br />
5、不要坚持错误不改。<br />
6、要分清对话与只准自己讲话的区别。<br />
7、对话要有记录。<br />
8、尽量理解对方。<br />
<br />
遵守这样的对话规则，对话才有效。# 另外，我觉得罗伯特议事规则里的规则非常有必要推广一下，不管是开会、辩论还是其它的集体讨论，都应遵守一定的规则，就像普世价值一样。否则就会陷入焦油坑，越使劲挣扎越是毫无头绪，甚至淹没掉自己的理性和平和，走入情绪化的泥潭，不但没有辩论出个所以然来，反而伤了和气。我对此深有体会，和有些人对话时，他们偏要给你抬杠，抓那些无关宏旨的细节，偷梁换柱，预设观念，栽赃陷害，甚至人身攻击。如果大家在交流沟通时，能够遵守一定的规则，应该会减少很多麻烦和噪音，至于那些争强好胜、爱逞口舌之利的家伙，心里骂一句SB、不理睬他们也就行了，毕竟我们还是需要一些宽容精神的……<br />
<br />
遵守这样的对话规则，对话才有效。<br />
<br />
最近数日，有几位我所尊敬的知名网人和他们的支持者，在饭否和Twitter上，互相骂阵，而且忘记了他们是在一个公共场合这样做，――这就好像有人天天在你们家客厅吵架，你想谈点正经事都不行。<br />
<br />
我不关心几个男女之间的事，我关心的是中国互联网上的论辩伦理和交往伦理，尤其关心在网上可否推行上述的对话规则。这事之所以重要，是因为我们在现实当中无法实行这些规则。网络上好不容易拥有的一块实验田，数位也号称网上意见领袖的精英，其意气用事如此，实验如何进行得下去？<br />
<br />
我尤其在意骂阵中出现的这句话：“除了我认识的人之外，我一直都是谁关注我我就关注谁。希望那些主动关注我，在我关注你们之后又取消关注的人告诉我一声，以便我取消对你们的关注。”回声室里的声音，是不能当作大自然中五音杂陈的天籁的。<br />
<br />
不过想想，任何一个人群聚集之地不都会这样？或许我还是给互联网投射了过多的理想色彩吧。这也算是中国第一代触网者的宿命。<br />
<br />
好处是，这些网络上的意见领袖，现在让大家得以近观，明白人非圣贤，“To err is human，to forgive is divine.”同时，也验证艾未未先生说的一句话说得好：“不要对我有妄想。”如同饭友mostarich 所说：“美女也臭脚，所以不要把一切都想象得太美好。”<br />
<br />
更根本的问题在于，我们需要什么样的网络意见领袖？转吾友萧强对网络活动中的“代表性人物”或者”发言人”的几点观察，用他的话来说，都是“引玉之砖，并非深思熟虑的定论”：<br />
<br />
1，他／她拥有发言的平台，比如：博客。（这条现在技术上已经不是问题了。被封杀除外。）<br />
2，他／她的信号（言说）应当是非常基本的，可以形成“身份认同”的言说。不仅仅是就事论事，技术或者技巧层面的论理。<br />
3，他／她的信号（言说）应当不仅仅是修辞的，而是身体力行的。在很多情况下，是为之付出常人没有付出的“代价”的，不管这代价是时间，金钱，还是自由。<br />
4，他／她最好有一技之长，是某种“专家”。现代社会人人都有分工，人比较信“专家”的话。<br />
5，他／她的私人品行也要经得住不仅是大众的八卦眼光，还包括政敌的攻击。网络时代更是私事容易公开化，所以公信力很容易被其他事情瓦解掉。<br />
<br />
如果用这几条去衡量的话，在纷乱复杂的网络活动中，虽然没有“组织”去领导，但是真正的影响力也不是容易建立的。“发言人”也不是好当的。<br />
<br />
或许还要加上第六条：学会倾听。毛向辉说：“不去倾听，也会丧失公信力。”</div>

							
							
							
							
