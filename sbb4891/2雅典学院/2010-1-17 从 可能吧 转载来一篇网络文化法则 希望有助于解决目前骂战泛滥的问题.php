网络文化背后的法则<br />
<br />
木贼<br />
2010-01-01 C 11:01 am<br />
133条评论<br />
<br />
下面介绍的网络法则：逻辑通常不是严谨的，有的还很荒谬，它们仅仅是一些总结，感想或幽默，也许能提醒你陷入无意的骂战，或是加强某些看法。有句话讲，能广为流传的名言都是极端偏见的。<br />
<br />
如同学习历史中常见到的：”飞鸟尽，良弓藏；狡兔死，走狗烹”。网路也有类似的现象，可能就是这些不断重复出现的现象，加强印证了此类法则。总之，You won’t find them here, but they certainly exist. 知道这些法则或许能：1.行为预测2.有效讨论 3.吸取教训 4.娱乐恶搞5.摆正心态。<br />
<br />
<br />
<strong>一. 高德温法则（Godwin’s Law）</strong><br />
<strong>当初Godwin是这样假设的：任何时候，当你把他人或他人的行为跟希特勒做比较对照时，你就”丧失了论据”。</strong><br />
<br />
<br />
<strong>后来衍生成网络版，定义如下：<br />
<br />
在在线讨论不断变长的情况下，把用户或其言行与纳粹主义或希特勒类比的概率会趋于一（100%）<br />
<br />
为什么会出现这样的现象？你可以想象，讨论中，一旦把带有邪恶的纳粹形象引入，不仅有侮辱对方的意思，还把讨论引向主观式的对错判断，而且比照的逻辑就是个谬论。当讨论渐长，不耐烦的人也渐多，攻击出现的概率倍涨。<br />
<br />
早期的Usenet，在讨论中，如有一方纠缠到纳粹问题，play&nbsp;&nbsp;”the Hitler card“，即宣告话题结束。没什么好谈了，论证变为主观性了。<br />
<br />
<br />
<br />
你看到了，很多时候，把别人比作希特勒，相当于一种恐吓论据，其逻辑也是谬误的。在讨论中，有人故意用纳粹结束话题，也是备受谴责的。<br />
<br />
国内网路上，很少用纳粹类比，通常代以另一个极端疯狂的事例，文革和脑残。典型的如：”你丫就个红卫兵，跟文化大革命时没区别”，SB脑残等。<br />
<br />
从论证观点转换到主观性判断，由坚守阐述自己的观点转到挑对方的毛病，交流变成了诋毁。所以，我把国内的高德温法则表述成下图：<br />
<br />
这图要加个前提，不适用troll类论坛，新闻跟帖。像mop，帖子专门找抽的，新闻跟帖有著名的二楼定律（自古二楼出傻逼）。</strong><br />
<br />
<br />
<strong>二. 坡法则（Poe’s Law）</strong><br />
<br />
不是诗人爱伦坡的法则哈！poe’s law指的是：<br />
<br />
没有表情的示意，或是其它明显的幽默表示，不可能创作一个讽刺原教旨主义的仿拟作品（parody），同时其他人又不会误解真实的含义。<br />
<br />
坡法则有两层意思，第一层是嘲笑原教旨主义者的观点，说明一些原教旨主义者的看法是多么的极端和荒唐，以至于连讽刺性模仿文（parody）不能分辨区别；<br />
<br />
第二层则是说一些网民，无论讽刺作品做得多么的夸张荒谬无逻辑，某些时候，某些人总会上当，信以为真。<br />
<br />
国内网路上也会经常性见到这样的情况，当你写出一篇支持政府的反讽作品时，真正的愤青会信以为真，而不是愤青的，则会理解讽刺文章的真实含义。<br />
<br />
造成这种情况，就是因为某些人的极端原教旨主义倾向。而坡法则，最初是来自讨论宗教时的争执，后来多数的使用范围也是在宗教和政治方面。<br />
<br />
可能你会说，我怎么没发现自己有某种偏见倾向？确实，单靠自身很难发现，更多是从别人身上认识自己。<br />
<br />
除此之外，你可选择试下哈佛大学的内隐联系测试，内隐联系测验能测量那些人们不愿意或没能力报告的内隐态度或看法，更好更全面的了解自己的偏见。<br />
<br />
说来，我们每个人身上都存在着或多或少的原教旨倾向，维舟在《人人都是原教旨主义者》中写到这个的原因：<br />
<br />
我这么说是基于这样一个现实：任何人类思维都具有原教旨倾向。所有理论都倾向于造成一种自我完善的体系，可以运用它来解释和把握整个世界，最终它成为一个人看待和衡量世界的眼镜和尺子。<br />
<br />
可见，当你偏见的时候，你还是会努力的维持完善自己的偏见，寻理由支援。<br />
<br />
下面这个出色讽刺仿拟例子，就结合了高德温法则和坡法则。讽刺了把希特勒做类比的逻辑谬误，同时又耍了原教旨主义者，他们很可能分辨不出原意，真以为是讽刺欧巴马：<br />
<br />
