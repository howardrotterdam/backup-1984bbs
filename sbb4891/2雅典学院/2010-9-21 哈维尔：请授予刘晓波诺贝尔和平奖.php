													<h2>哈维尔：请授予刘晓波诺贝尔和平奖</h2>
						
						
																											<div id="postmessage_839225" class="t_msgfont">布拉格――想想真是难以置信：三十年前，我们这242名关心捷克斯洛伐克人权的公民，聚集到一起，联名签署了《七七宪章》。这份宣言呼吁共产党关心人权，并清楚地声明，我们不愿再苟活于政府的威压之下。<br />
<br />
我 们的成员五花八门：前共产党员，天主教徒，基督徒，工人，自由派知识分子，艺术家和作家们走到一起，用一个声音说话。我们因对这个政权的不满而联合起来， 因为这个政权要求公民无时不刻的服从：商店店主被迫贴起宣传标语，‘全世界的工人们，联合起来！’。孩子，学生，工人被组织在五一劳动节游行。行政人员必须在每天工作开始前谴责美帝国主义。公民们在选举中“投票”，而唯一的候选者是执政党。<br />
<br />
共产党们向来喜欢分而治之。《七七宪章》发表后，政府使尽手段来打散我们的组织。我们被监禁，其中四位被判刑7年。政府也常在小处找我们麻烦（包括暂停我们的驾照和没收打印机）。特务组织监控跟踪，搜索我们的住处与办公地，官媒捏造事实来攻击我们，试图搞臭我们的运动。但这种攻击只能使我们更加团结。《七七宪章》鼓舞那些在沉默中受难的兄弟们：你们并不孤独。最终，那些《七七宪章》提出的主张在捷克斯洛伐克实现了。民主改革的浪潮在1989年席卷了东欧。<br />
<br />
我们怎么也不会想到，在三十年后的中国，我们的宪章听到了回响。2008年12月，303名中国人权活动者，律师，知识分子，学者，退休政府官员，工人和农民签下了他们的宣言：《零八宪章》，呼吁政府实行宪政，尊重人权，进行民主改革。《零八宪章》诞生于世界人权宣言的六十岁生日。尽管中国政府竭尽全力使它不出现在电脑屏幕上，《零八宪章》还是通过互联网找到了它的受众，它的签名数最终超过了10000人。<br />
<br />
就像七十年代的捷克斯洛伐克一样，中国政府的反应迅速而粗暴。几十上百名签名者被请去“喝茶”。十几名所谓“头目”被逮捕。签名者的职业升迁被停，研究资金被断，出国申请被拒。报纸和出版社将所有签名者列入了黑名单。最严重的是，著名的作家与异议人士，《零八宪章》的主要起草者刘晓波被捕。他已经因为支持1989年天安门广场的和平请愿活动而被关押了5年。被捕后，刘被羁押了一年，期间与妻子或律师的会面受到限制，并最终因煽动颠覆罪受审。2009年12月，他被判11年徒刑。<br />
<br />
尽管刘晓波身陷囹圄，他的思想却难以束缚。在《零八宪章》里，刘晓波描绘了另一个中国与实现她的路径，它挑战了党霸占改革话语权的底线。它鼓励中国的年轻人积极参与政治，倡导法制，宪政和多党民主，并且开启了一系列关于如何实现这些目标的讨论和文章。<br />
<br />
就像在七十年代的捷克斯洛伐克一样，宪章最重要的意义，是为不同的群体搭建了前所未有的沟通桥梁。在《零八宪章》发表前，“我们只能活在分离，孤立的状态下”，一位签名者说。“我们并不善于向周围人表达个体的经验。”<br />
<br />
刘晓波和《零八宪章》正在使这种情况得到好转。<br />
<br />
当然，《零八宪章》面对的政治环境与七十年代的捷克斯洛伐克有很大不同。对经济增长的探求，使中国现实的特殊情况与传统的共产主义国家极不相同。尤其是对年轻的城市白领来说，中国可以说是处在后极权时代。当然，中共还保留着许多不能触碰的禁区，而就在创造《零八宪章》这一先驱行动中，刘晓波打破了其中最不可触碰的一块：不许挑战中共的政治垄断，不许将中国的腐败，工潮，猖獗的环境破坏与政治改革的停滞联系起来。<br />
<br />
刘晓波使两者的联系看上去无比明确，为此，他坐了十几年的牢。也许是怕关押他的监狱成为政治抗议点，当局心术不良地强迫刘晓波远离他的妻子刘霞和朋友们居住的北京，转到东北的辽宁服刑。<br />
<br />
刘晓波也许被隔绝了，但他不会被遗忘。下个月，诺贝尔和平奖将会宣布2010年的获奖者。我们请求评审委员会向刘晓波颁授此奖，使他成为第一名获此殊荣的中国人，以表彰他二十年来采用和平方式，坚定地倡导改革。通过颁授此奖，诺奖委员会将向刘晓波和中国政府传达一个信号，那就是：中国和世界上的许多人，将与刘晓波携手并肩，继续为13亿中国人的自由和人权坚定奋斗。<br />
