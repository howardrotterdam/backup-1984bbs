显然的，老鸨的意思就是那层膜。“因为男人的处女情结，女人破了处女膜还得找人缝补，好好的肉体去做手术。因为有了处女情结，很多女人被强奸了不敢报案，因为只要没人知道她被强奸过，处女膜一缝补照样可以骗男人。有些女人被强奸之后，所愤怒的不是自己是被强行暴力的，而是因为自己的身子破了。”------如果仅仅只是那一片“膜”而已，那么，史上不会有《铡美案》了吧？铡的可是个男人----陈世美。<br />
很多打着民主自由旗号，批判中华文化并自诩为精英的家伙其实根本不懂什么叫“中华文化”。老鸨就是其中之一。（带过，另一个话题了。。。。。。。）<br />
回头再说说这“处女情节”吧。如果仅仅只是一层膜的问题，男人可是没有“膜”的。铡美案就不会发生，可是它发生了。为什么?显然皇家不能容忍的不是陈是不是，有没有老婆，（历代驸马基本一夫多妻）而是“欺君”！什么是欺君？不纯！臣伺君以纯。换句现代语就是：你是我唯一的爱。处女情节源于唯一。<br />
我不知道为什么老鸨会认为“有严重社会危害性的，是中国人的贞操观，以及根深蒂固的处女情结。”难道对唯一和执着的追求是一种罪过？或者，他的“贞操观”建立在一层膜上？？？<br />
也许，比这更罪过的还有呢！这个李银河的学生疯得更加离谱！“兽交有什么社会危害性？基本上没有！兽交的受害者是野兽，并不需要给它人权，哪怕强奸了兽也是动物权益的问题。”<br />
就老鸨类的“民主人士”的所谓人权而言，这话没错。因为老鸨自称“自由或终极自由人士”。------一个不知道人格的“人士”而已！老鸨这样的“人士”可以变人也可以变兽。它已经超越了“人世”，这样的“境界”老夫肯定无法企及了，我猜想，我们多数人无法在人类和兽类中自如变换，当然，共党类的除外了，他们是“特殊材料”制成的。。。。。。你既然是人兽合体的，我们当然自愧弗如了。<br />
不过，老鸨似乎很鄙视动物呢。。。。<br />
“本人对动物权益一向不重视，重视了动物权益，难道让老子一辈子吃素啊？难道得了蛔虫不吃药把它毒死，还要我开刀做手术取出蛔虫放生啊？强奸一头畜生，或者诱奸一条宠物狗，都不是什么了不起的事情。文革期间，有个人被判刑，罪名是不仅奸污女知青，还糟蹋社会主义母牛，这罪名现在很少见到了。”<br />
也许，人带有动物的源码，你觉得有点耻辱吗？还是很可耻？<br />
你不进化我们进化。。。。。。。<br />
