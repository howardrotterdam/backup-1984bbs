													<h2>柴静：而我却今天才知道他的存在</h2>
						
						
																											<div id="postmessage_538699" class="t_msgfont">吾狂衅覆灭，岂复可言，汝等皆当以罪人弃之，然平生行已在怀，犹应可寻，至于能不，意中所解，汝等或不悉知。<br />
<br />
看史料的时候，会有一种感慨―-在动荡不安的中国大地上，只要给他们一点点空间，中国知识分子能在石缝里栽种下什么？<br />
<br />
八十年过去了，他在空白处栽种的一切，让我这样的后代得以生活在一个浓荫蔽头的世界上，而我却今天才知道叶企孙先生的存在。<br />
<br />
一<br />
<br />
回来的飞机上看书，看到这张照片的时候，我愣了一会儿，我不认识这个人，只是觉得很少见到这样恬静沉毅的脸，真好看。<br />
<br />
看完才知道，我们这些知道李政道，钱学森，钱三强，王淦昌……的人，原本都应该知道他―C他是他们的老师。<br />
<br />
李政道大二的时候，是他破格选送去美国，当时李政道才19岁，穿着短裤去办护照，办公的人员都不相信“怎么会是个儿童？”李政道后来说“他决定了我的命运”<br />
<br />
华罗庚是初中生，是他让在清华算学系任职，又送去英国深造，华罗庚说“我一生得他爱护无尽”。<br />
<br />
那是战乱烽火时代，但后来的重要科学发展所依仗的这些人，是他在那时满地焦土上栽下的桃李。<br />
<br />
―――可是我为什么不知道他？<br />
<br />
二<br />
<br />
深夜里我一点点找他的资料。<br />
<br />
他生在上海，父亲是旧式文人，让他从小读经史子集。<br />
<br />
他幼年已经以君子“慎独”之道要求自己，修身自省，对跟朋友之间“因小故而致割席”之事也写在笔下：“一时之忿，至今思之，犹有隐痛。”<br />
<br />
他讷于言，但一生都保持温润如玉的君子之风。<br />
<br />
1915年，他在清华上学的时候，成立清华校史上的第一个学生团体C科学会。<br />
<br />
每两周一次科学报告会，轮流作。“范围极广，如天演演说、苹果选种、煤，无线电报之设备、测绘法、力、废物利用，等等”<br />
<br />
他当时不过十七岁，拟订的会员守则是：（一）不谈宗教，（二）不谈政治，（三）宗旨忌远，（四）议论忌高，（五）切实求学，（六）切实做事。<br />
<br />
那种青翠的朝气里，满满的是中国大学的刚刚起步的生机。<br />
<br />
三<br />
<br />
1918，他留学美国，后来在哈佛读博士，导师是诺贝尔物理奖获得者布里奇曼。<br />
<br />
他的第一个研究课题，是用X射线短波极限法精确测定基本作用量子h值。实验结果，在美国《科学院院报》和《光学学会学报上》发表，很快被国际科学界公认为当时最精确的h值。<br />
<br />
这一数值被国际物理学界沿用达16年之久。<br />
<br />
这一年他23岁。<br />
<br />
四<br />
<br />
他27岁回国清华执教，很清楚自己要面对的是什么。<br />
<br />
他的学生回忆“第一届学物理的有4个人，第二届只有两个人，第三届只有一个人。从一年级到二年级，到三年级，都是他一个人教的，所有的课都是他一个人开，不是他想一个人单枪匹马．是他想请人家来，人家不来，也请不到．”<br />
<br />
他已不求收获，只问耕耘。<br />
<br />
他执教之严也是出名的，他的课给李政道的分数只是83。他允许这学生不听自己的课“因为你看的参考书比我的更高明”，但是“你的实验做的不认真，要扣去25分”<br />
<br />
他去世后多年，亲人发现他一直留着当年的那三张答卷，写在泛黄的昆明土纸上。<br />
<br />
五<br />
<br />
看史料的时候，会有一种感慨―-在动荡不安的中国大地上，只要给他们一点点空间，中国知识分子能在石缝里栽种下什么？<br />
<br />
他是清华物理系主任，这对他自己来说其实是一种牺牲，相当于要放弃了自己的专业研究来作行政的工作。因为他把聘任第一流学者到清华任教列为头等大事。<br />
<br />
从1926年到1937年，他先后为物理系和理学院聘来了熊庆来、吴有训、萨本栋、张子高、黄子卿、周培源、赵忠尧、任之恭等一批学者。<br />
<br />
吴有训还只不过是刚到校的普通教师，资历年纪都不如他，他把吴有训的工资定得比自己还高，1934年，他引荐吴有训接替自己的物理系主任一职。四年后，他力主吴有训接替自己的理学院院长一职，那时他正当盛年。<br />
<br />
冯秉铨毕业的时候，他对他们说：“我教书不好，对不住你们。可是有一点对得住你们的就是，我请来教你们的先生个个都比我强。”<br />
<br />
他不光要栽种，他还要育土。<br />
<br />
他在1929年又组建了清华理学院，其中包括算学、物理、化学、生物、心理、地学6系。<br />
<br />
他说凡是出人才的地方，必然是科学文化最盛行、科学土壤最肥沃、科学气氛最浓厚之地。比如欧洲的哥廷根、慕尼黑和美国的芝加哥等。<br />
<br />
中国科学研究停滞数千年，第一次有了这滚热得烫手的雄心：”除造就科学致用人才外，尚谋树立一研究科学之中心，以求中国之学术独立。”<br />
<br />
六<br />
<br />
那点嫩芽，是硬生生从石头底下顶上来的。<br />
<br />
清华的校史有纪录“早年的清华隶属北洋政府，实行的是校长个人专权，校长多为官员政客，既无多少学问，更不懂管理，且校长更替十分频繁，严重影响了教育教学工作的正常进行。”<br />
<br />
1927年，清华成立教授会和评议会。教授会由各科系教授组成，教授会成员投票选举各科系主任。评议会由评议员组成，评议员由各科系推举的教授担任。<br />
<br />
第二年，他当选评议员，当时他不满30岁。这个改革，就来自“少壮派”的推动。<br />
<br />
日后清华校史的研究者说“教授治校，说白了就是拒绝外行人进入学校管理层，把不懂<br />
<br />
科学、不闻学术、不谙教育的人扫地出门，它防止了旧制度下官僚体系对大学教育的侵蚀和破坏，同时把学校的行政权作分散化处理，形成相互制衡的机制，在保障高等院校的民主办学、民主管理，保证学校的独立、学者和学生的思想自由，以及激发创造力方面，发挥了不可磨灭的作用。”<br />
<br />
从1929年至1931年的两年间，清华没有官方委任的校长，纯粹由教授会代表全体教授治校。<br />
<br />
当时教授会的宣言是：”清华并非行政机关，学校完全可以超出政潮，独立进行”<br />
<br />
钱学森是他的学生，了解了这段历史，就会知道，钱学森去世前的遗问，不光指向未来，也是一次拼力的回头一望。<br />
<br />
七<br />
<br />
他终身未娶，唯与学生亲厚，当中有一人叫熊大缜，是他人生里最深的一段感情。<br />
<br />
网上可以找到熊当时的照片，生气勃勃，可以跃纸而出。他们在那几年里几乎相依为命。<br />
<br />
1938年，熊突然对他说要去冀中抗日。<br />
<br />
他明知这学生在河北没有依靠相熟的人，又没有政治经验，但是国难当头，他只能送他去，熊走后，他曾“约有十余天，神思郁郁，心绪茫然，每日只能静坐室中，读些英文小说，自求镇定下来。”<br />
<br />
他唯一能安慰的一点，是他能够帮着自己的学生在后方搜购一些雷管，炸药等军用物资，<br />
<br />
看这书时，我才知道，曾经炸碎日军机车车头的TNT药性地雷，是来自熊所在的“技术研究社”的制造，而不是我们小时看的电影《地雷战》中由农民土法制成。<br />
<br />
1939年，国共关系恶化，熊大缜被疑心是国民党特务，秘密逮捕，在没有调查核实，没有经过法定程序的情况下，在押送途中被用石块砸死。<br />
<br />
从平津来冀中参加抗日的知识分子将近百人受到株连，在这之后，因为没有科技力量自制弹药，冀中的战士在一段时期内只能拿着空枪，把秸秆塞在子弹袋里作战。<br />
<br />
1947年6 月23日，他的日记里写“今日是旧历端午节。每逢端午，吾想到大缜。九年前的端午，他从内地回到天津，那是一个surprise。谁知道以后的事多么可悲。近几天在读《白石道人歌曲》，看到他的‘五日凄凉心事’句，更增悲痛。<br />
<br />
八<br />
<br />
建国后他仍然当过一段清华的一把手，一直到1951年。<br />
<br />
1968，他已经七十岁，因为熊大缜的事，涉嫌“国民党C.C特务团”被捕。<br />
<br />
他在狱中一年半。<br />
<br />
看过提审纪录的黄延复说，他所有的话，其实只有一句”我是科学家，我是老实的，我不说假话”。<br />
<br />
之后他由红卫兵组织隔离审查。<br />
<br />
他出现幻听，认为有电台在监视他，“一举一动都有反映，他喝一口茶，电台就说他喝茶不对，他走出门，电台就叫他马上回去”<br />
<br />
他的侄子看着他，“甚觉悲哀”，说“你是学物理的，你知道电波透不过墙，根本没有这种事，是幻觉”<br />
<br />
他说“有，是你耳朵聋，听不见”<br />
<br />
之后他再次入狱，出来的时候，已身患重病，小便失禁，双腿肿胀难以站立，整个身子弓成九十度。<br />
<br />
九<br />
<br />
当时的中关村一带，有不少人都看过他，他穿着一双帮裂头缺的破棉鞋，有时到一家小摊上，向摊主伸手索要一两个小苹果，边走边嚼。<br />
<br />
如果遇到学生模样的人，他伸手说“你有钱给我几个”<br />
<br />
所求不过三五元而已。<br />
<br />
后来他已经渐渐恢复一些神智，有一次钱三强在中关村的马路上碰到他，“一看到老师呢，就马上跑上去跟先生打招呼，表示关怀，先生一看到他来了，马上就说，你赶快离开我，赶快躲开，以后你见到我，再也不要理我了，躲我远远的。”<br />
<br />
钱三强当时是二级部的副部长，负责原子弹工程。<br />
<br />
他的学生深知他的用意“他知道这么重要的工作，最忌讳同那些政治上有问题的人来往的，他生怕钱三强因此遭到一些不幸。”<br />
<br />
十<br />
<br />
两年后，在北大作教师的张之翔骑着自行车，在校外的一所公寓中找到了他。<br />
<br />
张之翔说“他已经不认识我了，我说我是张之翔阿，他说哦哦，坐坐。他坐在藤椅上，就给我看，这个腿，两个腿肿得很厉害，走不了路。他也没有牢骚，很平静的。可是人已经不像个人形了。我也没有多少好说的，我说先生多多保重，我就，我就…”<br />
<br />
他泪流满面。<br />
<br />
“…我就离开了，以后再也没有看到他”。<br />
<br />
他的侄子说他从没对任何人讲过自己的悲惨，“他的看法好像是世界上和历史上冤枉的事情很多，没有必要感叹自己的人生”<br />
<br />
他只是经常坐在一张旧藤椅上，读点古典诗词或历史书打发时光。<br />
<br />
1977年1月13日，他去世。在生命的尽头，钱临照去看他时，他取出《宋书》来，翻到范晔写的((狱中与甥侄书》中的一段：“吾狂衅覆灭，岂复可言，汝等皆当以罪人弃之，然平生行已在怀，犹应可寻，至于能不，意中所解，汝等或不悉知。”<br />
<br />
十<br />
<br />
我反复念他这几句话“吾狂衅覆灭，岂复可言，汝等皆当以罪人弃之……”<br />
<br />
一直到八十年代，已经平反之后，清华想要为他塑像之时，仍有人说“你们要为这个人造像，我就尿它”。<br />
<br />
“然平生行已在怀，犹应可寻……”<br />
<br />
1929年，他在一篇叫《中国科学界之过去、现在和将来》的文章里说“有人怀疑中国民族不适宜研究科学，我觉得这些论调都没有根据。中国在最近期内方明白研究科学的重要，我们还没有经过长时期的试验，还不能说我们缺少研究科学的能力。惟有希望大家共同努力去做学研究，五十年后再下断语。诸君要知道，没有自然科学的民族，决不能在现代立脚得住。”<br />
<br />
八十年过去了，他在空白处栽种的一切，让我这样的后代得以生活在一个浓荫蔽头的世界上，而我却今天才知道叶企孙先生的存在。<br />
<br />
“至于能不，意中所解，汝等或不悉知……”<br />
<br />
这张照片上，他是如此坦白温和地看着我，不求理解，不加责问，但这样的疑问，却从此重重放在了人的心头。<br />
<br />
（哈林和钱烈宪提醒， 叶先生在哈佛的研究项目普朗克常量，应该小写斜体，我原写为H,已经改过，谢。另有朋友留言说《宋书》第一句应是，“吾狂衅覆灭”，我原文写为覆天，打字错误，改过，抱歉。）</div>

							
							
							
							
