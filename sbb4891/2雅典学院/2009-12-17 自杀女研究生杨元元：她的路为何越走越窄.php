<br />
在理想与现实之间<br />
<br />
&quot;在大学过去将近一半的时候，她对当初的理想念念不忘，便开始自学法律课程，并酝酿考法学研究生。&quot;<br />
<br />
湖北枝江，一个封闭的小县城。杨元元在这里度过了不算愉快的童年――父亲早逝，一家三口靠母亲为工厂看大门的微薄收入维持生计。<br />
<br />
但生前为化工工程师的父亲还是给杨元元姐弟留下财富――有趣的故事和读书的理想。还在幼儿园时，杨元元就喜欢提着一个塑料袋扮小学生。<br />
<br />
父亲离世后，只有小学文化的望瑞玲带着姐弟俩挤进一个暗无天日的斗室，望瑞玲对那段时间最深刻的回忆是：墙壁整天掉泥。<br />
<br />
有一天，杨元元回家告诉母亲，她日后要念法律，“为穷人作主”――某个当地领导的孩子通过关系，夺走了原本属于她的“市三好学生”荣誉。但1998年高考填志愿时，望瑞玲拒绝了女儿到大连学海商法的请求，理由是考武汉的大学可以省些路费。<br />
<br />
“元元当时都快求我了。”望瑞玲有点后悔当年的固执己见。最终杨元元屈从，报考了武汉大学经济学专业。但从此耿耿于怀，在写给弟弟的一封信里，她这样开头：“你以后不要听妈的……”<br />
<br />
家里多一个大学生，望瑞玲二百多元的月薪日渐变得捉襟见肘，要靠助学贷款和亲戚资助勉强支撑。<br />
<br />
对杨元元的投资按理是值得的。只是在杨元元进入大学的次年，以拉动内需为主旨的中国大学扩招和教育产业化热潮掀起，“精英教育”走下神坛，但这场改革同时带来节节高攀的学费，还有每况愈下的就业前景。<br />
<br />
扩招引发的困局仍需数年才显现结果。贫困生杨元元当下最关心的是生存问题，由于不忍向家里要钱，她开始超负荷地接家教和兼职，还常常为省路费而步行往返。不少当年的同学对她印象深刻的一幕是――常常夜晚在饭堂擦桌椅，或者把垃圾扫起来并从中拣出卫生筷。<br />
<br />
生活阶层的落差以及私人时间的稀缺，杨元元变得不怎么引人注目。来自城市的同学衣着新潮，而她的衣服大多是亲戚所赠，且单调陈旧。大多时候她对此并不介意，只是一次看完文艺表演，极其少有地在日记里表达了对那些漂亮彩妆的羡慕。<br />
<br />
在大学过去将近一半的时候，她对当初的理想念念不忘，便开始自学法律课程，并酝酿考法学研究生。<br />
<br />
2001年家里发生了两件大事，一是弟弟杨平平考上武大，二是原来居住的军工厂要搬迁，母亲失去住处，一夜间变得无家可归。<br />
<br />
母亲拎着家什出现在杨元元的大学寝室。床太窄，母女就侧身而卧。杨元元上课的时候，望瑞玲就弄些茶叶蛋和豆腐干到教师楼前卖。数月后，望托一个老师的关系，住进了一间只有一张桌子的闲置房，学校也持默认态度。<br />
<br />
很多迹象表明，大学后期杨元元的心情变得越发郁闷，除了来源于越发排斥的本专业外，更来源于变得现实而具体的家庭压力。她的生活如钟摆般精准而机械：上课，家教，帮母亲摆摊……她几乎没有朋友，连亲友都羞于走动，她把兼职的所有收入悉数交与母亲代管和支持弟弟求学，直至毕业后5年才偿还贷款，赎回毕业证和学位证。<br />
<br />
情况在2002年毕业那年继续变坏。一是她的校园爱情无疾而终；二是成绩第一的她却被挤掉了保研名额――据信是被人做了手脚；最后她在委屈和愤怒中参加考研，又失败了。<br />
<br />
路越走越窄<br />
<br />
&quot;她开始在《红楼梦》里寻找自身悲剧的影子。“她说她像晴雯。”表妹望妍说，“心比天高，身为下贱。”&quot;<br />
<br />
糟糕的现实开始危及生存，杨元元需要一份工作。<br />
<br />
但在2002年夏天，抱有同样想法的大学应届生直逼150万，他们充斥着各地招聘场所，拿着再不熠熠生辉的大学文凭，不断调整期望值。但纵然如此，当年仍有大量应届毕业生找不到工作，数目庞大的“大学毕业生低收入聚居群体”由此催生，现在，他们被称为“蚁族”。<br />
<br />
在相当长时间里，优秀毕业生杨元元很不幸也成为了其中一员。望瑞玲陪女儿投递简历吃惊于她的表现。“她大概有些清高，放下简历就走，也不和人家说话。”后来母女俩找了一个月租300元的老房子，消耗着微薄的积蓄。<br />
<br />
对于一个经济学专业毕业生来说那一年可能并不友好，2002年正是“熊市”当道，全球经济颓势初现。杨元元连一份凑合的本专业工作都找不到。无所事事了几个月后，她进入一家培训中心当英语老师，教幼儿英语，月薪800元，每天两个小时地来回武昌和汉口。<br />
<br />
武汉理工大学背后的一个跳蚤市场是小贩热衷光顾之地，一到晚上，杨元元就去接望瑞玲的班，她从不吆喝，坐下来就看书，思考文学和法学，中途休息的时候又非常市井地记下一天的消费，事无巨细，精确到分。<br />
<br />
房东李峰见过几次这对母女，他感觉“她们生活很封闭，而且不太懂人情世故”。“有一次楼下反映厕所漏水，有些生气，我说由我出钱修一修，但她们拒绝了，说嫌麻烦。”李说。<br />
<br />
兼职做家教的时候，一位学生家长想和杨元元拉拉家常。杨显得不太乐意，搪塞了一下就走了。“我感觉她有些自卑。”<br />
<br />
这种自卑像一扇屏障裹住杨元元。毕业很长时间里，她都没有配手机，几乎与所有同学都失去联系。“她该如何开口介绍近况呢？”弟弟杨平平说，“说还在做家教吗？”<br />
<br />
挫折感，焦虑感，封闭倾向，成为无数个像杨元元一样的“蚁族”标签。他们眷恋大城市，憧憬好工作，都在等待创造传奇，但越发激烈的城市化，人口结构转变，劳动力市场转型等因素又抵消着他们的努力。这个已逐渐具备社会化意义的阶层，让一代人的青春在夹缝中渐渐失衡。<br />
<br />
杨元元认可这种等待的一个例证是，她曾考取了两个外省小城市的公务员，但最终决定放弃，一是距离远，二又不是“北京上海”，而后者正是母亲从小教育的真正值得落脚的地方。20岁的时候，望随单位调动在上海工作了5年，她喜欢这个洋气十足的大都市，几句蹩脚的上海话仍让她保持着不错的优越感。<br />
<br />
为实现大城市梦，杨元元也尝试过打破困局。毕业后她连续三年考研，均无果。她开始在《红楼梦》里寻找自身悲剧的影子。“她说她像晴雯。”表妹望妍说，“心比天高，身为下贱。”<br />
<br />
2004年，杨元元阴差阳错地接了一份保险推销员的工作，但帮亲戚办了几份之后就卖不动了。她的业绩如此糟糕，以至于现在已没有同事能记得起她。<br />
<br />
事业上最后一丝激情消耗于2005年。由于受轰轰烈烈的大学生创业潮触动，杨元元倾尽积蓄，与人合伙办一份文艺杂志。一开始信心十足，还租了个像样的办公室，但仅坚持了半年，由于办刊思维的不合时宜，杂志基本滞销。“连我也不看。”望瑞玲说。<br />
<br />
贱卖一屋桌椅后，杨元元也懒得慨叹命运。她开始像上了年纪的人一样把未竟的愿望嫁接他人。弟弟杨平平成了这样的寄托者。本科临毕业时，一向温顺、保守的弟弟打算留本校读研究生即可。杨元元不同意，像母亲当年安排她的未来一样，她为弟弟树立了不容分说的目标――上北大研究生。她亲自搜资料，问内情，递表格，连专业也为弟弟选好。<br />
<br />
杨平平后来保北大直博成功。或许一同感染了喜悦，2007年开始，杨元元第四次酝酿考研。次年，她接到了上海海事大学海商法研究生入学通知。<br />
<br />
外面的世界<br />
<br />
&quot;入学之初就她问辅导员能否将母亲安置在校内，对方建议写封申请书，杨元元照办，但写着写着就哭了起来。&quot;<br />
<br />
已是而立之年的杨元元非常高兴人生第一次真正把握了自己的命运。而望瑞玲理所当然地认为“要跟着女儿去”。杨的舅舅提醒姐姐是否考虑过元元的终生大事，望的回答是“我们楼上三十好几没结婚的多了”。<br />
<br />
没有迹象表明杨元元也非常在意自己的婚姻问题。毕业之后，她再没有发展过其他对象。“她说要等有了事业再考虑。”一位亲属说。<br />
<br />
事实上，杨元元也的确在践行这样的想法。到新学校后，她勤奋和本分。“每次上课都独自坐第一排。”同学赵伟说，“现在还有这样的研究生吗？”<br />
<br />
作为中国第一大城市，拥有1300多万人口的上海庞大得超出母女俩的想象。现在还说它是“冒险家的乐园”仍没有错，但只相对于那些积累丰厚的上层精英。对普通人来说，生活的艰辛在这里加倍显现，犹如一个巨大而沉重的感叹号。<br />
<br />
杨元元和望瑞玲不舍得租昂贵又偏远的出租屋，宿舍就很好，还有全天候热水。像大学时候那样，杨元元和母亲每天挤在小床上睡觉。白天杨去上课，望就坐在田径场边织毛衣。她认识了一些学校家属，也从侧面领略到大城市的门户森严。“找份杂活还要有关系。”她说。<br />
<br />
大约一个月后，同宿舍的同学主动搬走了，房间成了杨元元母女专用。这个封闭的空间隔绝了她和同学间的交流。“她好像一项集体活动都没参加过。”辅导员吴志毅说，“每次她都沉默地跟在母亲的背后，听她母亲说话。”“带母上学”虽然引起一些小范围的议论，但很快就被习以为常。杨元元还是觉得有些不安，入学之初就问辅导员能否将母亲安置在校内。对方建议写封申请书，杨元元照办，但写着写着就哭了起来。“她不爱把困难告诉别人。”望瑞玲说。<br />
<br />
等了一周仍无消息，杨元元母女就去找学院领导，说家里有困难，能否解决母亲的住宿。“我和元元一直请求他，说武大当初也安排住处了。结果他说，‘没钱，没钱读什么书？’”望瑞玲说，“回去后元元很受伤，说这里没有温情。”<br />
<br />
入学后，杨元元认识了一个北京的同学，很快就当她是挚友，无话不说，但对方却只是礼节性地应付。后来，杨请求往她宿舍放些母亲的东西，对方拒绝了，杨转身就走，自此再也没有说过话。“大城市来的等级观念强，就连学校也按照原来本校还是非本校分班。”同学魏玲说，“但杨元元认为这样侵犯了她的尊严。”<br />
<br />
后来，杨元元接过辅导员的几个电话，说最好把母亲安置在校外。望瑞玲没怎么放心上，坚信学校一定会管。“我看到宿舍楼还有很多空房子。”<br />
<br />
11月21日，杨元元宿舍突然来了两个宿管，限令她在半个小时内搬走母亲的所有东西，以后不许再来。望瑞玲看到女儿当时神色有些紧张，不停地赔礼道歉。随后就冒雨带母亲出去找房子，学校地处偏僻，一天搜索无果，最后花了100元住了宾馆。望说女儿心疼得睡不着。<br />
<br />
第二天，她们找到了一处房子，但当天拿不到钥匙。杨元元因为要排练节目，望瑞玲就叫她回去，说她能找到落脚的地方。当晚上海气温骤降到只有4度，望瞒着女儿在学校礼堂前坐了一夜。<br />
<br />
天亮后，杨元元知道后非常自责，当即就坐在地上要母亲趴在她身上睡。晚上，母女俩拿到了钥匙，进去后发现是毛坯房。两人和衣躺在地上，抱在一起取暖。<br />
<br />
杨元元整夜都向望瑞玲道歉，说这么大了也没把母亲安排好，明天就找学院领导商量。过了一会，杨似乎想起什么，说“算了”。<br />
<br />
“不可以被打败”<br />
<br />
&quot;她从一堆书里抬起头来，半是交流半是自语的说出那句：“人可以被毁灭，但不可以被打败”。&quot;<br />
<br />
矛盾24号全面爆发。当天上午，望瑞玲和女儿在宿舍收拾衣物。半个小时后，一位年轻宿管看到望瑞玲的登记，跑上来指着杨元元说：“你妈要是再来，你就拿不到毕业证和学位证，你将来什么也没有。”过了一会又指着望的鼻子骂：“你这个乡下来的农村老太婆，不要把你农村的那一套拿到这里来。”<br />
<br />
一直忙着赔礼的杨元元听到这里脸色变得难看起来。在回去出租房的路上，杨元元又向母亲道歉，说现在还没让她享清福。<br />
<br />
由于持续的担心、焦虑、愧疚和疲劳，此时杨元元已严重缺乏睡眠。11月25日清晨，杨元元突然从被窝里坐起来，语带怨气：“凭什么不让我们住，我要找领导。”接着她又说：“都说知识改变命运，我学了那么多知识，也没见有什么改变。”<br />
<br />
望瑞玲觉得有些反常，跟女儿闲聊起来，平复她的心情。杨元元开始把从小到大的事情细细回顾了一遍，这一次她大胆地对母亲说，如果当年支持她报大连海事，现在一切都好了。杨还特意说起她做家教时一个自杀的15岁女孩，仅仅因为学业压力从28楼跳了下来。她反复叨念着：“她出身这么好，可惜了，可惜了。 ”<br />
<br />
望瑞玲不知道该说什么好，杨又让母亲陪她练《罗密欧与朱丽叶》，其间杨元元有点怅然若失，说起当年一个让人佩服的学长。然后她抱着头，说脑袋乱了。<br />
<br />
到下午的时候，望催女儿上课，杨元元说“不去了，没意思”，然后她提议把租房的钱拿回来去玩。这种让人担心的状态到傍晚时分终于平静下来。晚饭后杨元元还拉着母亲到宿舍洗澡洗头。<br />
<br />
当天晚上，杨元元跟同学排练话剧，其间她说了几次不想演了，怕丢脸。但一出场彩排的时候，她又丝毫不差地念出台词，表现堪称完美。11点，她甩着长辫子向同学们说再见，并约好明早起来再练一次，然后进了宿舍，再也没出来。<br />
<br />
而当晚，弟弟杨平平在深圳用QQ给姐姐留言，商讨母亲安置的问题。他的建议是：春节后把母亲接到他那。而杨元元很可能没有看到这些内容。“我很快就毕业了，能照顾母亲了，但姐姐却走了。”<br />
<br />
杨元元的死震惊了学校。大批记者闻讯而至，关心着这个平日无人问津的女研究生。校方惊讶于事态扩大之迅速，关停了招待所，并派保安人员监视记者行踪。事件的官方说法是：学校按规定劝离杨元元母亲，并从人性化角度提供房源帮助。整个抢救过程也是及时和尽责的。同时，校方还暗示杨元元有患抑郁症的可能。<br />
<br />
“总之，学校在这件事情上已经仁至义尽。”上海海事大学党委宣传部部长彭东恺说。<br />
<br />
而杨元元家属则坚持认为宿管人员态度恶劣是杨元元自杀的诱因，而且抢救当天个别人员相互推诿，延误了时间，应负相关责任。家属先向学校索赔35万元，后降为16万。<br />
<br />
在校方要承认他们在这起事故上有责任的前提下，一位家属说，这笔钱准备为望瑞玲在宜昌买套二手房。<br />
<br />
12月15日，一个阴雨连连的早上，杨元元的遗体在上海南汇区殡仪馆火化。对于望瑞玲来说，现在唯一能纪念女儿的是一堆法律书籍和课堂笔记。她的文化不足以阅读它们，她也未必完全弄懂女儿多年的执著。“我对她还是了解太少。”<br />
<br />
而杨元元的表妹望妍，则在这天夜里回忆起一个阳光灿烂的下午，表姐翻着海明威的《老人与海》，她从一堆书里抬起头来，半是交流半是自语的说出那句：“人可以被毁灭，但不可以被打败”。<br />
<br />
（应受访者要求，杨平平、陈亮、李峰、赵伟、魏玲、望妍为化名）</div>

							
							
							
							
