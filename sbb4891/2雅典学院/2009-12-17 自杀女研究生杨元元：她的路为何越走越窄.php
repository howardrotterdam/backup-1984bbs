<br />
30岁的上海海事大学特困生杨元元死了――她用两条毛巾自缢于宿舍盥洗室内。此前，她想带着生活无着的母亲一起住在宿舍，但学校拒绝了这一要求。她无力另租房屋，母亲因此在冰冷的学校礼堂台阶上睡了一晚。<br />
<br />
11月24日晚，一个言辞尖刻的宿管员再一次挑战她微薄的尊严――驱赶其母亲并威胁让她“拿不到毕业证”，她感到羞辱和愤怒，但奇怪地保持了沉默。<br />
<br />
没有人意识到这是一次警告。两天后，这个终其一生试图通过克制和倔强来保持内心高贵并努力改变自身命运的人，终于在长期的贫困、冷漠、无助和自责中不堪重负，以极其痛苦的坐姿自缢于宿舍的盥洗室。<br />
<br />
她的宿舍需年缴费1300元，比走廊另一侧的房间便宜 200元――因为背阴，见不到多少阳光。<br />
<br />
种种迹象表明，进入上海海事大学攻读研究生的两个月，是杨元元多舛与吊诡人生的一次回光返照。在这里，被命运胁迫的她完成了人生仅有的两次自决――不断读书以求改变命运，却最终屈服并结束生命。<br />
<br />
从未放弃奋斗的自杀者<br />
&quot;<br />
“她从未放弃过奋斗，却在曙光将现时谜一样退场了。”&quot;<br />
<br />
2009年11月26日上午，海商法研究生杨元元不选择自杀的话，她的任务将是上午8点半在班上出演话剧《罗密欧与朱丽叶》中的女主角。<br />
<br />
但直至近9点，同学等来了坏消息。杨元元被发现自缢于宿舍盥洗室内，场景恐怖：杨背对洗手盆呈坐姿，一条枕巾和一条毛巾首尾相接绑在洗手盆的水龙头上，绕过她的脖子并深陷皮肉中。她的腿微屈伸着，支撑着悬空的身体。<br />
<br />
惊慌失措的发现者们用小刀割断了毛巾。首先赶到现场的是海大保卫科的陈亮，他把杨元元抱到地板上平躺，进行心肺复苏。此时她已摸不到脉搏，泡沫从嘴角冒出来。两分钟后，陈亮用筷子把杨紧闭的牙关撬开，发现舌头呈耷着，并且发乌。“看是救不过来了。”陈说。<br />
<br />
一些胆小的同学哭了起来。此时，杨元元的母亲望瑞玲，正被学生拉到对面房间，哭喊着女儿的名字――从女儿本科起，她们就形影不离地生活了8年，两个月前，女儿带着她来这里继续求学。<br />
<br />
送到医院后，杨元元的心电图已是一条直线，随即被宣布死亡，时间是上午10时。<br />
<br />
沉默寡言的杨元元以常人难以想象的方式带来轰动。海事大学随即发布了官方声明，称已尽了一切能力帮助杨氏母女，急救和善后工作也是规范和人道的。<br />
<br />
但望瑞玲和亲属的描述却截然相反，她们坚信是学校持续地、不留情面地驱赶杨母，才让杨元元背上沉重的道德枷锁，走上绝路。此外，校方人员救助不力也是制造悲剧的根源。<br />
<br />
这绝不是命运和这个女子开的唯一一次玩笑。在短暂的30年时间里，杨元元在现实的夹缝中所表现出的“坚强”和“要强”同样突出――成长于单亲家庭，自立完成学业，4次考研失败，度过了长达8年毫无成就感的不堪岁月，沉重的家负，带着母亲走到一个尴尬的年龄，甚至在自杀的前一天，她还担心朱丽叶演得丢人现眼，排练到深夜。“她从未放弃过奋斗，却在曙光将现时谜一样退场了。”一位生前好友这样总结杨元元的一生。<br />
<br />
杨元元自杀的宿舍盥洗间　图/南方周末记者 叶伟民<br />
