													<h2>杨元元这个鸡蛋必须自缢</h2>
						
						
																											<div id="postmessage_378801" class="t_msgfont">　　&quot;假如这里有坚固的高墙和撞墙破碎的鸡蛋，我总是站在鸡蛋一边。&quot;<br />
　　<br />
　　村上春树耶路撒冷演讲中的这句话广被引用，其实&quot;它&quot;还很年轻，不到1岁。的确，支持&quot;鸡蛋&quot;是先天的政治正确，我们义无反顾地支持自缢在上海海事大学宿舍卫生间中的女研究生杨元元，无论这个个案背后有怎样的吊诡，这些都不重要。<br />
　　<br />
　　但是，支持杨元元，并非等同于支持她所作出的人生抉择，并非等同于支持她的母亲和弟弟，而是出于支持我们自己，如村上春树所说，每个人都是高墙下的蛋，抑或如崔健所说的红旗下的蛋。每个蛋壳都很脆弱，但总有更加脆弱的那个。<br />
　　<br />
　　在目前的情形下，我的判断是，杨元元必须死，没有生还的侥幸。例外的情况只有2种--墙不那么硬了，或者蛋更有韧性。<br />
　　<br />
　　杨元元之死背后复杂的人情社会、丛林法则是个既幽深又简单的事实。幽深到我们永远也无法搞清其中具体的是非，因为，最重要的当事人已逝；简单到每个人都能感受一二，我们都身处其中，我们都是丛林竞赛中干掉杨元元的胜利者（包括杨母和杨弟），我们都更加了解、并更能适应这整个体制，因为我们还活着。<br />
<br />
<br />
　　现在，杨元元之死中能够确定是的关键事实是，一是杨死前对&quot;知识改变命运&quot;一语偏执的理解；二是杨家整体的经济状况还未到山穷水尽（但杨元元个人经济状况可能已经破产）；三是杨家母女同住的有违常伦的生活方式；四是学校的管理的失职。<br />
　　<br />
　　这四点尽管是个案，仍具有相当的普适意义。<br />
　　<br />
　　其一，&quot;知识改变命运&quot;是整个社会畸形的教育观念带给杨元元的。在杨元元的判断里，即社会宣传的判断里，&quot;知识&quot;被等同于&quot;上学&quot;，&quot;改变&quot;被进化成&quot;改进&quot;，简单来说，变成了&quot;想致富，先上学&quot;。这是一种类似战时共产主义宣传口号，有力量，有效率，也有巨大副作用，比如造成杨元元之死。<br />
　　<br />
　　诚然，知识会让整个人类对社会的认识更加全面，但对于个体来说，知识未必是改善生活的捷径，<br />
　　<br />
　　更具体到杨的个案，我国的本科和研究生教育中的学费倒挂进一步加深了对&quot;知识改变命运&quot;的歧解。众所周知，我国的研究生教育仍有相当比例的公费生，也就是说，尽管读研究生很穷，但是，解决温饱还不是很困难，甚至生活压力比读本科小。这让杨元元看到了通过读研改变命运的途径――考上公费研究生――熬过这两年――找到好工作改善生活。<br />
　　<br />
　　事实上，读研并非义务教育，学问只是解决温饱之后理想主义者的乐园。说得更极端点，像我本科时的导师所言，研究生就应该是富人读的。这里的富人其实还是指经济上解决了温饱，不指望很快依靠学问来赚钱的人。我这位导师主张研究生教育应当与西方接轨，全面收费。从他的角度出发，这样一定会带来更好的生源。<br />
　　<br />
　　然而，我国的本科教育，即使是公费生，也需要缴纳相当高昂的学费，这与免费的研究生教育形成倒挂。如果国家真的认为&quot;知识改变命运&quot;，需要改善国民整体知识水平，那么请把本科教育变成职业教育，全面免费。而对于研究生教育，则因设立门槛，不需要再巨额补贴，让那些有钱有闲有志于改变人类命运的人去读吧。穷人读书不乏极端的例子，除了杨元元，最近还有北理工的&quot;博士退学记&quot;。<br />
　　<br />
　　收费的本科教育让杨元元背上了沉重的经济包袱，免费的研究生教育又让杨元元错误估计了读研的经济困难，是这个体制让人一错再错。<br />
　　<br />
　　其二其三两点涉及个人隐私，不好多加臆测。但我个人认为，这也是造成杨元元自杀重要原因。与学校的矛盾、对教育的失望只是单方面的原因，还好化解，但如果背腹受敌，两面受压，则是一个人精神崩溃的最大可能。<br />
　　<br />
　　其四，学校的处理是让人寒心的，这个整个体制行政能力低下、整个社会道德底线低下的表征之一。学校没有替杨元元解决个人问题的义务――这也是对其他同学的不公平。但肯定有更好的处理方法，不要让杨元元背负的多方压力迅速激化。如果学校处理得更艺术，哪怕不能解决，但做好解释工作，杨元元可能不会这么快自缢。遗憾的是，学校没有。杨家还提出了宿舍管理员可能延误了抢救。这一切，其实我们每天都在目睹很多次--体制对个体的冷漠，发达对落后的冷漠，以至于鸡蛋对鸡蛋的冷漠。<br />
　　<br />
　　某种意义上，杨元元必须死，宿舍纠纷只是偶然的一个导火索，也可能是其他的导火索。她所生活的社会和家庭造就的她的性格注定她在这个社会中生存不下去，现实和教育是悖行的，家庭和现实也是悖行的。杨元元只能期待一个更和谐的社会才能活下来，但是，在那个更和谐的社会中，可能杨元元可能已经不是现在这个杨元元了。<br />
　　<br />
　　&quot;我们每一个人都或多或少分别是一个鸡蛋，是具有无可替代的灵魂和包拢它的脆弱外壳的鸡蛋。我是，你们也是。&quot;村上春树说。</div>

							
							
							
							
