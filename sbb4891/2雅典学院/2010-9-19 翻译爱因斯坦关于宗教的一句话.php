																											<div id="postmessage_837127" class="t_msgfont">这么有名的人，百度一下不难吧？<br />
本周，阿尔伯特・爱因斯坦从坟墓里走出来，为科学和宗教之间的文化战争火上浇油。 <br />
<br />
此信写于1954年，是物理学家寄给同为犹太裔的哲学家艾瑞克・古特金的。爱因斯坦在信中称《圣经》“相当幼稚”并嘲笑了“犹太人是‘上帝选民’”这一观念。此信在伦敦一家拍卖行以404，000美元的高价售出，是售前估价的25倍。 <br />
<br />
美联社援引了布鲁斯伯雷拍卖行总裁鲁珀特・鲍威尔的话，后者透露这封信不明身份的买家对理论物理及其遗产充满强烈的热情。另据《卫报》报道，在诸多未成功的出价人当中包括以直言不讳著称的无神论者、牛津大学进化生物学家理查德・道金斯。 <br />
<br />
404,000美元使这封致古特金的信成为爱因斯坦最畅销手稿之一，卖价仅次于1996年纽约克里斯蒂拍卖行为爱因斯坦与第一任妻子米列娃・马利克53封情书合集所卖出的442,500美元。在同一拍卖行，爱因斯坦和他最好的朋友米歇尔・贝索的一纸尝试计算性论文目标拍卖价为398，500美元。该论文后来成为爱因斯坦登峰造极的成就―广义相对论的关键部分。 <br />
<br />
加州理工学院爱因斯坦档案工程主任、历史学家戴安娜・L・克莫斯-布彻韦德说，为研究爱因斯坦的学者们都知道这封致古特金的信，她对卖出这样的高价并不感到吃惊。 <br />
<br />
她在一封电子邮件中写道：“这封信阐述了爱因斯坦对宗教、犹太教以及对上帝和宗教教义极其重要的思想和观点。”她还说，爱因斯坦写这封信的目的虽然不是为了发表，但是它“简练、质朴”，比他在公开场合下通常采用的暗喻方式更加直言不讳。 <br />
<br />
同样，哈佛大学自然科学历史学家、长期研究爱因斯坦的专家杰拉尔德・霍尔顿也认为拍卖价格不出意料。他说，在过去几年里，由于媒体对相对论100周年纪念的大肆宣扬，包括《纽约时报杂志》评选他为20世纪“世纪伟人”以及几部新传记问世，爱因斯坦的卖点不断提高。霍尔顿博士称此信为“一个壮举”，“雄辩、精炼地表达了自己的信条”。 <br />
<br />
正如爱因斯坦在其自传中记述的那样，他12岁就失去宗教信仰，认为宗教是彻头彻尾的谎言，并且绝不动摇。但是他从来没有失去对宇宙可见秩序的宗教感，或者是他尽情享受宇宙奥妙和神秘的直觉。爱因斯坦曾经说：“宇宙最难以捉摸的东西就是它的可理解性”。 <br />
<br />
同样是在1954年，爱因斯坦在另一信中写道：“假如在我内心有什么能被称为宗教的话，那便是我对科学所能揭示这个世界的结构，怀着无限的敬仰。” <br />
<br />
爱因斯坦一贯认为，能对祈祷做出回答、人格化的上帝是个天真的念头，而死后重生是痴心妄想。但是他每每提及上帝―作为解释物理学法则的隐喻。在他著名的反驳量子力学的言论中，他说过“上帝不玩骰子”；他在字里行间无休止地重复他那句名言：“没有宗教的科学是跛子，没有科学的宗教是瞎子。”所有这些已经足以让某些幻想家把他划入宗教信徒的阵营，甚至不久前还把他描画成智慧设计论的鼓吹者。 <br />
