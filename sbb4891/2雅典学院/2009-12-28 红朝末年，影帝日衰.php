													<h2>红朝末年，影帝日衰</h2>
						
						
																											<div id="postmessage_402448" class="t_msgfont">《烧饼演义》<br />
红朝末年，影帝日衰。 时年重聚百官，以立后事。 帝擢辽国公李小强入朝，拟承帝位。 这辽国公原本亦寒门仕子，第科举入燕园，为翰林院伺读学士，后入红i团，与帝共事厉、德、愍、僖四帝，有经天纬地之才。帝知其学高能重，甚爱之。后帝位三公，强领河南总督，封一等候。<br />
<br />
帝即位，强官居二品，封辽国公。 择日临朝，朝臣奏请立储之事，内阁学士王深宁表辽公之德，众臣知宁乃影帝第一谋士，不敢妄言。唯国师道：“臣未敢忘‘京郊之盟’矣。” 帝视公子党众人皆有愠色，怒而不发，谓国师道：“爱卿意下若何？” 国师献太上意旨，道：“先皇在时，每言闽国公习不平大才。平乃御史大夫习成公之后。成公侍奉我朝始皇并憨、献、厉、德、愍、僖七帝，德才广布甘陕并东南各省，居功至伟；闽公耿忠良善，性宽仁厚。任福建、浙江两省总督时，勤政省心，颇有祖风。前松江巡抚陈善宇勾结商贾中饱私囊，交大理寺查办时，松江百姓皆有怨。闽公受命亲往抚之，泽被苍生，民莫不感其恩德。立闽公为太子，则我朝江山万世矣。”<br />
<br />
帝自思良久，徐许之，下诏立闽国公习不平为皇太子，册封平王，领武英殿大学士兼翰林院编修；立辽国公李小强为皇长子，领太傅兼御史大夫，助太师温虚相协理朝政。 国师见太子当立，凑请告老还乡。众臣称善，不复言。<br />
<br />
话说这公子党中有一人，乃户部侍郎薄启来。来本是体仁阁大学士薄安公之后。安公行伍出身，随始皇倡义兵，监军陕甘，封开国广威将军。后弃武从文，官拜户部尚书。愍帝时，有八老摄政，安公首辅。幼时，来恃父威，性情放任，尤好犬马。少领辽东青泥洼知州，领能人巧匠开山石、兴土木，填东海以建新城，其城雄浑辉煌状若金山，固皆称“金州”。来又领诸匠于金州筑西洋音乐喷泉广场无数，世人皆赞“欧罗巴不能及也”。后擢辽东总督，再入京师，行走户部，官拜正二品侍郎。来此番入朝听封，以为升天，不想得巴国侯领江州巡抚，官至西南去了。 来问阴公：“吾不及闽公乎？” 阴公道：“闽公少时，侍奉兵部尚书翟穆公，粗通兵法，公诚不及耳。” 来大怒而去。 且说巴国侯帐中一谋士，侍来久矣，深知其志，进言曰：“世有一清虚道人，青城山张天师高足。后远赴西洋，深得道法，云游四海，行踪莫测。吾闻得此道近日在江州万县境内。公有大事，可亲往问之。”来善其言，当夜轻车简从，至万县。 那道人见巴侯有齐天异相，以君臣之礼待之。来受之大喜。时来问卜，道人以指当空画圆，来不解其意，问之，道人不答，复画一圆；来仍不解其意，道人再画三圆乃去。 侯惑而不言，随道人至后堂密室。 道人谓巴侯曰：“小道不才，观侯有天子之相，斗胆代万姓启侯智。” 遂献天书《烧饼梦》十二卷，言：“参破此书者可得道。” 侯大喜，取天书一卷，看道： （引子）开辟鸿蒙，谁为王种？都只为位高权重。趁着这解放天、改革日、和谐时，徒表愚忠。因此上，演这出你死我活的烧饼梦。 （终身误）都道是和谐盛世，俺只念民主法制。空对着、金銮影帝折腾胡；终不忘、神州泪相千岁温。叹朝中、人言梨园今方服。纵然是防民之口，到底谁低俗！ 阅至此处，巴侯突发奇疾，胸中翻腾，肝胆欲裂，其状甚苦。道人急念咒救之，自思：“实非救国新主也。” 巴侯新愈，道人画一道鬼符放入锦囊，再取芝麻烧饼一只，空其内，将锦囊嵌入饼中，交予巴侯曰：“小道之前当空所画者，烧饼耳。此饼乃镇国之宝，持饼者有掌玺之运。” 侯既得此饼，大喜而去。<br />
<br />
早有厂公告于帝曰：“巴国侯结道议政，于空中画五环，人莫知其故。” 帝亦不解其意。有理藩院侍郎吴立民，早年使西洋，颇有才识，听闻五环，谓帝曰：“此乃夷人奥运盛会耳。”帝甚喜，命理藩院照会各国使节，次年八月奥运于京师。 然帝知满朝文武鲜有善外事者，遂托太子平总理奥运，暗思“平若有失，朕必废之”。<br />
<br />
为行奥运，帝发民夫，征民膏，聘西洋工匠筑各式会馆，与京郊建大观园一座，名“奥运村”。不数日，立国技宫一座，名“有凤来仪”。又兼造“火立方”、“六颗松” 等等楼台若干。 然自筹奥运以来，国事危矣。<br />
<br />
年初，南方大雪，国中道路结冰，有数百万乡民困他乡无还，帝祈天，太师下泪；<br />
<br />
三月，吐蕃僧变，帝从内臣之计，内患外引，昭告天下：“此乃妖僧鸠摩智祸国耳”。<br />
<br />
四月，安徽童病，患五千余人；帝发五省良医救之；<br />
<br />
五月，蜀地大陷，死伤数十万人。帝亲领六军去救，太师复下泪，谓灾民曰“多难兴邦”；<br />
<br />
六月，贵州wen安民变，有冤民聚数万焚县衙，贵州总督挥师平乱，言“乱民皆不明真相矣”；<br />
<br />
朝中上下疲于奔命，更兼奥运临近，臣民具称红朝有亡国征兆耳。 有臣启凑，言鸠摩智亡西域时，曾做妖法，咒我朝逢九必乱，今距妖僧西亡四十九年矣。请领中原高僧作法于朝堂，灾祸可免。 帝素知鸠摩智本是宁夏人士，幼时赴吐蕃学法数十年，终得大果，自号“国师”。始皇在时，发兵征吐蕃，鸠摩智领众僧御天兵而不敌，败走天竺。其后四十九年，屡秘计独之，不得。鸠摩智屡游历西夷，以弘法之名传红朝暴政，恃精深佛法惑西夷之众。夷人敬之若佛家教皇，视红朝诸君为商纣隋炀。然红朝通商四海，西夷有求，怒而不言。 帝谓诸臣曰：“家奴作乱，王师可平；友邦反目，天威何存”，遂准，令五台山文殊院智幸方丈、普陀山普济院智裕方丈、九华山法华寺智望方丈及峨眉山报国寺智胜方丈入朝，弘法护国。 自此，幸、裕、望、胜四大高僧齐集京师，作九九八十一天大法，保奥运安矣。<br />
<br />
时京师奥运，万国来朝，叹红朝雄壮，赞影帝圣明。帝大喜，表太子、太师、众高僧并文武百官之功，不在话下。 然皇长子强恐太子平得势，不喜，谓帝曰：“若如此，儿无望矣。” 帝着心腹议之，王深宁献计：“夫天朝上国之君，理政于内，结夷于外，吾观辽公领御史大夫，多有劳苦，其功与太子筹奥运之功无异。今鸠摩智于海外诽议内事，夷对我朝颇有微词。可遣太子使之，太子不通外务，必出差错。” 帝善其言，使太子平于南美，至墨西哥，夷甚慢之，太子怒，责夷人曰：“世有夷人，饱而甚闲。我朝一不输革命，二不输饥贫，三不诽夷政，尔奈何群而谤我乎？” 帝喜，遂命人造势朝堂，责太子愚；又令侍卫扮作布衣平民，讥讽于市。自此太子势渐微矣。<br />
<br />
不数日，帝密招辽公入朝，商废立太子之事。辽止之曰：“平不足惧，公子党虽势众，但多不弄权谋，甘为商贾。然出入朝堂者尚有一人，除之，方可再图大计。”言毕，提笔写一“来”字，帝称善，废立之事少提。 却说巴国侯领江州巡抚薄启来，自得了清虚道人之饼，不日加爵，号“渝国公”，大喜，于江州风水极盛之处石桥铺筑高台，曰“饼营”，日夜香火供奉。 来听闻太子平失势，料鸿运至矣。为彰其志，令神乐署署丞录革命老歌廿八，传于江州小儿日夜诵之。世皆以为始皇复生。又常恨阴公言其不善兵事，差各州县衙役当街操演，凡遇蓄须散发鸡胸驼背痔疮脚癣体臭及走路外八字者，皆拿而问之。数日，囚者数万人。公笑曰：“江州安矣。”<br />
<br />
时有历愍二帝京师之乱时所余反民，结“民主教”，奉夷人“普世价值”为尊，聚三百当世名士著《08宪*章》一卷，请改祖制，民多有从者。 龙颜大怒，言民主邪教勾结乱党、私通外夷、祸乱朝纲，密诏凡著解贩印传抄者，皆为叛逆。遂差锦衣卫拿人，囚而用刑，以正视听。又于云南筑刑宫一座，名“看_守所”，囚嫌与犯豺狼虎豹一室，人皆有去无还。典吏谓人曰：“此乃‘躲猫猫’耳。”民多有怨者。<br />
<br />
当是时，帝再施内患外引之计，复言“西洋有法兰西主欲会鸠摩智矣”。一时间海内山河震动，国人群起而攻，皆称吐蕃自古王土，夷不能图。如此三月，乱党诛而民怨平。 各地上表请平乱之功。唯江州不报，帝奇之，问渝公。渝公笑答：“江州未乱。”愈奇之，复问，左右皆言渝公治民有方矣。帝大喜，封安王，总督西南诸省军政。 时辽公生疑，密使人往江州，回报，具言清虚赐饼一事，辽公大惊，恐安王夺了上位，秘计除之。<br />
<br />
三月，太祖征吐蕃五十年矣，帝令各地设典纪念，各州府再征民膏，民愈恨。吐蕃各地密谋起事。 是日，辽国公入朝，见影帝，言“儿臣有一箭双雕良计，可灭安王、平吐蕃，但乞父皇借三等侍卫一人则矣。” 影帝笑：“儿府中猛将百员亲兵数万，竟向朕求一区区三等侍卫。” 辽国公复言：“儿臣闻父皇早年平乱，于沙场拾得吐蕃遗孤，收为义子……” 帝大惊，问：“儿乞洛桑次仁何用？” 辽公笑而不言，帝素知其多谋，遂准。 又十日，江州布政使王鸿擎飞书启奏，有吐蕃武士光天化日潜入石桥铺“饼营”，盗得宝饼，手刃护军，守备千总率外委把总追而不得，官民咸有惧色，皆称鸠摩智作乱。安王急火攻心，不省人事矣。<br />
<br />
又两日，辽国公李小强领洛桑次仁还朝，献宝饼，谓帝曰：“儿臣闻渝公得此饼时，道人曾告之曰‘此饼乃镇国之宝，持饼人有掌玺之运’。”帝得饼而视之，饼上刻有夷文： SB――dict*ator*ship<br />
<br />
着理藩院诸人录而译之，原是“独##裁烧饼”之意。 又闻得清虚曾藏锦囊于饼中，帝亲启之，锦囊上一串小字“遇胡则开”。<br />
<br />
帝大喜，拆而视之，囊内羊皮一卷，录《烧饼歌》两首：<br />
<br />
一<br />
<br />
古道残月烙金帛，泪满长街又如何。<br />
<br />
日破云涛星火处，谁认他国作祖国。<br />
<br />
二<br />
<br />
欲雪家仇焚腊肉，又恐高声惊王侯。<br />
<br />
若承父辈文武志，定叫神州作金州。<br />
<br />
当是时，帝头痛剧烈，忽见清虚道长跃然金銮殿上，帝大惊，令三营禁军死命拿之不得，吐血数斗，不复临朝矣。</div>

							
							
							
							
