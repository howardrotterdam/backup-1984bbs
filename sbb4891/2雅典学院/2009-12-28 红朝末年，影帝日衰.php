																											<div id="postmessage_402969" class="t_msgfont">烧饼演义――前传 （<a href="http://www.bullogger.com/blogs/libertechine/archives/286719.aspx" target="_blank">http://www.bullogger.com/blogs/libertechine/archives/286719.aspx</a>）<br />
<br />
前传<br />
罐罐三鹿都是奶，奥运实在精彩。汉唐哪有和谐拽。<br />
访民依旧在，几度中南海。<br />
弱冠流离香街旁，惯看人权宪章。两声民主就结党。<br />
红朝多少事，不在史书上。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; ――调寄《临江仙》<br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;红朝开元以来，共立八世：太祖毛始皇、刘憨帝、华献帝、邓厉帝、胡德帝、赵愍帝、江僖帝、胡影帝。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;始皇开国以来，几行废立，太师刘憨公、中南王林幽公皆未得善终。后传位献帝，托孤于大司马兵部尚书叶定公。然献帝暗弱实难服众，定公摄政，生废立之意。是定公与右丞相邓怀公秘计，献帝乃退，怀公自立为厉帝。后三年，厉帝耄耋之躯行“领导干部革命化、年轻化、知识化、专业化”，立王储胡德公为帝，上皇垂帘听政。缘哿年，上以缘郾乒，黜之，立赵愍帝。愍帝三年，缘坜埃有民数万乱京师继而波及王土，唯松江平吐蕃定。六月，国人动乱，丞相李戾公奉上皇旨平定京师，废仁帝。松江巡抚江灵公即位，号僖帝。太上效法康熙点乾隆，又擢吐蕃总督胡和公入朝理事，封平王，官拜“红青团主”，敕设隔代接班人。僖帝七年，太上崩，帝独揽大权，从此华夏遍行贪腐之风。僖帝十年，有fa轮教作乱，内阁学士曾熙红献计除之，帝甚悦，拜红“阴国公”。次年，夷人美利坚于斯拉夫岛袭我使馆，诛我族人，举国甚怒，有民自焚于闹市，请战，帝不许。世皆怨帝之羸弱而思始皇邓厉之强硬。坊间乃传：储君平王监军吐蕃，料鹰派耳。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;即大位十二载，帝尽用松江旧部于朝野，人皆曰“松江帮”。然帝自视外臣，任京师朝臣结党营私，未敢与之争。其王孙尽享祖荫，终日放荡无度，鲜有成事者。世人皆以“公子党”称之。平王久据“红i团”，凡入团而有经纬者，平王皆敬而结之，曰：“我为太子，卿可助我图大业。”众人皆曰“苟富贵，无相忘。” <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;是年，帝年迈，欲传位于阴公。阴公少有权谋，好诗书，识廉理。其父乃开国元勋、户部侍郎曾海，故阴公自幼出入宫门，深得为官之术，不若其余公子党众。更兼得阴公早年官至松江布政使，盖亦同党耳。自那日定计除了fa轮教，帝视之为左膀右臂。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;话分两头，这平王寒门仕子出身，短短数年，从甘肃从七品布政使都事一路扶摇直上平步青云位极人臣。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;想那平王雄姿英发，风衣墨镜，谈笑间xueshan狮子旗灰飞烟灭激起千堆雪，文治武功，何其雄壮，有诗为证： <br />
<br />
马下提笔考清华，马上拿刀砍喇（空格）嘛。<br />
笔做刀来刀作笔，马匿草时草腻马。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;平王如此英武不凡，僖帝都不由得暗暗称奇。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;据信僖帝好娱乐，懂音律，通洋文。传世语录“too simple，sometimes naive”和“If you take care of your father，father will take care of you”享誉海外，为无数仁人志士所拜倒。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;厉帝在时，以定诏“废除领导终身制”，僖帝衰微之躯，上不能违天命，下不能背祖训。帝郁郁寡欢，终日不喜。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;阴公早察，谓帝曰：“远效康熙，近学厉帝则矣”…… <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;帝大喜，从其计。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;僖帝十三年，红朝百官聚首京师，推贤良、议废立。帝下诏退位，称太上皇，龙虎将军刘杭木奏请太上掌印信，收天下兵权，统帅三军。再举平王为影帝，前松江巡抚、御史大夫吴安国、前朝御史大夫温虚相为太师；又有直隶总督京兆尹贾熙林、大学士阴国公曾熙红任太保；后松江巡抚黄竹任太傅，鲁国公吴民正与太子太保、协办大学士罗操任左右都御史，广东巡抚李长冬为文渊阁大学士。以上诸人温太师原为赵愍帝门生，其余皆为太上心腹重臣。诸臣组阁，影帝甚为不悦。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;帝即大位，移驾直隶平山县西北坡村，观先帝毛始皇遗址，叹道：“朕未尝不思先帝之志，”遂命国中各人先后研习“两个务必”、“保先”及“八荣八耻”种种经史典籍，然民多愚钝，不得要领。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;行年,帝擢各省“红i团”大员，尽任总督巡抚布政司，把持从辽东中原到江浙一带的地方行政。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;同年，广东总督张道江入朝启奏，时有英伦弃民于港岛九龙冥顽不化，抗拒天恩王法。帝命九龙知府董立华、协办大学士礼部尚书唐国璇及温太师先后赴港，皆无功还。时有人保奏太保阴公可使之。帝准。阴公使而港乱定。帝大喜，封号国师，称“盛世第一能臣”。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;影帝二年，广东瘟疫传京师，太医郎中皆无良方。帝欲发兵去救，奈印信不掌，王师难发。屡请上皇，监医患于小汤山，广征良医，疫乃平。民多有怨者。帝不悦。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;国师献计：“今太上恃兵摄政久矣，吾有一计可除之。”遂献东厂密卷《松江贪腐录》一册，谓帝曰“可广告天下，太上结党营私亏空国库，民必恨之。是以反腐之名去其羽翼，上势孤，必可南面治之。”帝善其言，命吴罗左右都御使奉旨，领禁军三千拿太傅黄闵公妻子囚于文渊阁，曰“双规”――在规定时间、规定地点交代问题。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;不数日，太傅黄公抱病不朝。国师亲往太上府中细说厉害，太上惊，欲发兵讨之，恐生民怨。不日，国师奏言“上质六军而挟天子，欲效康熙点乾隆。”初，帝不应，后闻得上皇帐中三员一品猛将领侍卫内大臣龙虎将军刘杭木、掌銮仪卫事大臣金吾将军张约男与兵部尚书骠骑将军迟章曹列阵京郊，甚惧之。遂御驾京郊，从上皇言，定“京郊之盟”，准太上立储。僖帝欣然，退太上皇位，封沪国公，号“三表老人”，袭一等爵，回松江养老去了。至此，松江帮偶有余党立于朝堂，皆唯唯于帝耳。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;然国师率公子党纵横朝野，帝未敢与之争也。</div>

							
							
							
							
