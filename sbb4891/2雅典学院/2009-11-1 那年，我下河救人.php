													<h2>那年，我下河救人</h2>
						
						
																											<div id="postmessage_308609" class="t_msgfont">我小时候下河救过人，虽然已经过了10几年，但是那情况我是记忆犹新。<br />
<br />
当时是盛夏，阳光猛烈，我们在田间的一个小河沟玩水，周围都是农田。由于一场炎热，周围空无一人，农民们都回家午休去了。<br />
<br />
这条河非常窄，顶多只有3米宽，其实是农田水利工程下出现的一条较宽的引水渠，水不深，最深处顶多就是2米，而且河岸边还有一部份是能踩到河底的。<br />
<br />
这么窄的河，绝不会想到有人会在里面出事，但就是这么窄的河，都差点淹死我和我的同学。<br />
<br />
事情是是这样发生的。<br />
<br />
我和常二会游泳，涛涛和雪雪不会，于是我和常二在河中间游，两个不会水的同学涛涛和雪雪在岸边玩。这个地方我们以前也来游过，所以比较熟悉，由于父母们都教育过我们不能下河玩水，我也很清楚涛涛和雪雪不会游泳，因此专门叮嘱他们不要往深水里去。<br />
<br />
水情很熟悉，河不宽也不深，不会游泳的也知道不往深处去，看起来，一切都很安全。<br />
<br />
玩了一会儿，我和常二回到岸边，常二这家伙平时就喜欢寄人鞋带把人推倒之类的恶作剧，他看到涛涛和雪雪在岸边打水仗，就悄悄跑到他们背后打算突然袭击。<br />
<br />
这个时候，不知道他出于什么心理，可能只是想来个恐吓式的恶作剧，他突然把涛涛推向了深水处。<br />
<br />
涛涛完全没有料到有这么一推，一下就被推离了岸边，脚下突然没底，人骤然就慌了。<br />
<br />
那种恐慌很难在平日里见到，完全是一种爆发式的恐慌，并且伴随着急剧而愤怒的尖叫：<br />
“我……日你妈呀！我不会游泳啊！”<br />
<br />
他的脸瞬间就扭曲了，充满了极度的恐惧，身体变得非常紧张僵硬，由于不会游泳，他只能无序而急速的胡乱挥动手臂，拍起一阵水花。<br />
<br />
我和雪雪一下就吓呆了，因为我们从来没有见过一个人急速的变化，常二则在一边哈哈大笑。<br />
<br />
很快，也就几秒，涛涛已经连续喝了好几口水，他嘴里的咒骂已经模糊不清，他渐渐沉了下去。<br />
<br />
常二这时候才发现有点不对劲，他的笑容完全僵住了，他也被这突然的变化吓呆了。但他很快有了反应，他赶紧游向涛涛，想把他拉回来。<br />
<br />
涛涛这时候已经完全恐慌了，常二一接近他，他就牢牢抓住常二，与传说中溺水者会牢牢抱住你的传说略有不同。涛涛把常二当成了树干，手脚并用的向常二头顶上爬去。常二只来得及发出“啊呜”的一声，头就被按到了水里。<br />
<br />
涛涛继续向上窜去，想让自己的脑袋停留在水面上，看起来他象是爬到了常二的肩膀上。常二则被牢牢的压在了下面，两人迅速往下沉。<br />
<br />
涛涛此时张大嘴拼命想吸气，但随即就又喝了一大口水，会游泳的人都知道，如果遇到了浪，连续喝几口水，很容易就喘不过气来，对于会游泳的人尚且如此，对于不会游泳的涛涛，那更是不可想象，他眼看就要神志不清了<br />
<br />
这个时候，我不由自主的也游向他两，此时他们离能站住的河边恐怕只有1米，我希望能马上把涛涛拉到岸边，常二会游泳，应该会立刻脱险。<br />
<br />
当我接近涛涛时，他象是发现了新的救星，扑哧一声就向我窜过来，一把将我也牢牢抱住，把我的脑袋按向水里。同时他拼命把脑袋伸出水平大口吸气。<br />
<br />
我则被他牢牢的按进水里，我也突然失去了呼吸的能力。<br />
<br />
这段时间可能顶多只有几秒，但我当时却觉得非常漫长，我感觉被困在水里好几分钟之久，肺里的空气眼看就要耗尽，但我却被涛涛死死的抱住，并且不断把我往深处按下去，那种绝望压抑的感觉直到现在我都记得清清楚楚。我第一反应就是，妈的，我就要淹死啦！<br />
<br />
涛涛由于连续扑腾喝了好多水，已经精疲力尽，我在水里拼命的挥动手臂，就在我的体力耗尽之前，终于挣脱了他。同时，我突然踩到了河底，<br />
而这救了我两的命。我猛蹬了几下，我俩缓缓移动到了河岸边。<br />
<br />
涛涛在浅水里爬向河岸，他的脸涨得通红，鼻涕和水不断从鼻子里喷出来，然后筋疲力尽的倒在了岸边。<br />
<br />
我和常二站在浅水里喘着粗气，我们都不敢相信刚刚发生了这么危险的事，我们三个都差点淹死了。<br />
<br />
雪雪在岸边惊恐的看着我们，脸色煞白，差点哭了出来。<br />
<br />
如此窄如此浅的一条河，都差点让我们三人淹死，即便我挣脱了涛涛，逃回岸上，涛涛也难逃一死。<br />
<br />
自此以后，我对下河救人有了深刻的认识<br />
那就是不要逞能，没有装备最好不要贸然行事，否则你多半都会被淹死。<br />
<br />
此外，我知道了教育的局限，虽然我们的父母亲属都多次教育我们不能去河里随便游泳，相信大家也都知道在河里不能拿不会水的人恶作剧，但这一切还是发生了。<br />
<br />
所谓意外，就是完全在你意料之外发生了，无论看起来多么安全，也照样会有意外。<br />
人也不可能一直都是理性的。他们会没有任何原因的突然给其他人造成巨大的伤害。</div>

							
							
							
							
