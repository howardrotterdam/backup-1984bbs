<br />
第二，这也是诺贝尔和平奖的发起人阿尔弗雷德 ・ 诺贝尔设立此奖项的初衷。为了推广中国的人权，政治改革和民主化进程，刘先生对宣扬自由博爱价值作出了巨大的贡献，这也是 100 年前的诺贝尔一直推动的思想。<br />
<br />
