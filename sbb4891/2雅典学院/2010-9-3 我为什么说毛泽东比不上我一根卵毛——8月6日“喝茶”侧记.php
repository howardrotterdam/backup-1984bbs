													<h2>我为什么说毛泽东比不上我一根卵毛――8月6日“喝茶”侧记</h2>
						
						
																											<div id="postmessage_810381" class="t_msgfont">荆楚<br />
<br />
2008年“手铐奥运”前夕，整个警方草木皆兵、风声鹤唳，神情异常紧张。恰于此时，网上朋友们搞起了一个恶搞节目――声言要发动全国人民到北京去做俯卧撑……他们也把我的小名签了上去。<br />
<br />
对此事情，在广西公安厅张处长找到我之前，我毫不知情。<br />
<br />
广西公安厅的国保警察一看到网上这个事情，且又牵涉到“荆楚”这个笔名，乃紧张得直冒冷汗。于是，张处长匆匆忙忙赶到桂林，向对我“刑事拘留”后并“取保候审”的主办警官问罪曰：“你们不是说对他已经转化了嘛！怎么搞的？他还在网上搞搞？”<br />
<br />
桂林的赵警官只好谦卑地回应曰：“这是网上的恶搞节目，没那么回事的……”<br />
<br />
但张处长却不依不饶，乃指责桂林警方“没有绷紧阶级斗争这根弦，麻痹大意，玩忽职守、疏于防范”云云。<br />
<br />
赵警官被逼无奈，有口难辨，只好跟他说：“我的理论水平低，实在没有能力来转化他……请处长大人亲自出马，去转化转化他？我们也好向领导学习学习！”（这些情形，是当天中午与赵警官一同午餐时告诉我的）<br />
<br />
于是这位尚未谋面的张处长，就匆匆忙忙赶到了全州，把我叫到全州博宇酒店的五楼客房中，对我“转化”了起来。<br />
<br />
寒暄客套一番之后，遂问及“到北京做俯卧撑运动”的事情。在我表示完全不知情之后，张处长才放下心来，遂开始了对我的“转化”程序。他用那一套党八股假大空的言辞，来对我“动之以情、晓之以理”地训导一番。<br />
<br />
我硬着头皮听完他一大通冗长乏味的说辞，顿时感到屁眼发麻，浑身在起鸡皮疙瘩。为避免折磨读者，今略举其大端，其大意有三：<br />
<br />
其一曰，这次奥运会是中国人的光荣，每个中国人都应为奥运会做贡献，给世界一个良好的形象……<br />
<br />
其二曰，要统一思想，统一认识，维护社会的稳定……<br />
<br />
其三曰，要坚持马列主义毛泽东思想，坚持四项基本原则……云云<br />
<br />
我强忍着听完他的训话后，实在感到恶心，乃恶向胆边生，就对他冷嘲热讽起来，并嗤笑他说：“如果相信这套荒唐悖乱的党八股假大空的话语系统，是脑膜炎后遗症，是四肢发达、头脑简单……假如中宣部说了一百句话，而你相信了一句，那么说明你是个傻子……”<br />
<br />
他乃追问我：“为什么这样说呀？”<br />
<br />
我回应他的第一个问题的是：“这样的奥运会，我不但不感到光荣和自豪，反而是感到羞耻！”<br />
<br />
他问：“……为什么？”<br />
<br />
我说：“第一，奥运会是一次体育竞技，也可以说是一种体育娱乐。这是富裕国家才玩得起的消遣节目。把一种竞技和娱乐节目拔高到爱国与不爱国的高度，只有那些神经错乱的人才会这样想。”<br />
<br />
“第二，这次北京奥运会，应该是中国政府向世界展示其文明形象的机会。而把奥运会变成了一个草木皆兵、风声鹤唳的运动会，变成了一个手铐奥运，变成一个法西斯奥运，使中国顿时成为一个警察国家，一个特务国家，几乎变成了一座人民的监狱，这不但不为中国人争脸，反而是给中国人丢脸。”<br />
<br />
“第三，当局为了举办奥运会，不惜干扰国内人民的正常生活秩序，给国内人民的正常生活制造了种种不便，甚至不惜大肆抓捕良心人士……就是为了讨好你们的外国爷爷。为了让你们的外国爷爷高兴，而不惜虐待国内人民。遂使北京人忍无可忍，喊出了要‘避孕’的口号。这叫什么事情嘛！”<br />
<br />
“第四，奥运会是富裕国家的竞技、观赏、娱乐性节目。而中国是一个穷国，应该把钱用在刀刃上，用在国内急需解决的民生问题上，用于解决国内人民看不起病、上不起学的问题上，用于解决失业下岗工人衣食无着的问题上，用于解决地震灾区的抚恤和重建问题上。举办这种劳民伤财的奥运会，是慷纳税人之慨，而不顾国内人民的死活。举办这样的奥运会，给我最直接的感受就是――打肿脸充胖子，是在茅厕里面安绣球，是死要面子活受罪……”<br />
<br />
张处长说：“那只是你个人的感受嘛！”<br />
<br />
我接着说：“你所说的统一思想，这是一句反人类、反社会、反文明的反动口号！”<br />
<br />
张处长忍不住说：“愿闻其详！”<br />
<br />
我说：“正因为人们有不同的思想，人类社会才有活力。如果一个社会的思想完全统一了，全都是用‘一种思想、一个脑袋’来思考问题了。那么，这样的社会，我只能说它是一个蠢猪的社会。这样的社会，就必然是一个‘一个发疯全民族遭殃’的社会。一个蠢猪的国家和民族，有什么前途！你说是不是？”<br />
<br />
张处长默然一阵之后，只好点了点头，并说“你说得有道理！”我才接着说：<br />
<br />
“中共中央屡屡提出统一思想，你不但木知木觉、习焉不察、习非成是，还要帮他们鼓吹呼号，我都为你感到脸红。”<br />
<br />
“‘在行动上保持一致’是说得通的。但这需要一个公开公正的民主程序，以少数服从多数的基本原则，也尊重少数人的基本权利……在制定政策前，展开充分的讨论，然后取得共识，然后贯彻执行……”<br />
<br />
“镇反肃反、三反五反、知识分子思想改造、反胡风、反右派运动之后，中国确实进入了高度的‘统一思想’的状态了。那时候，整个社会已没有任何批评质疑的声音了。听话的，就给饭吃。不听话的，连饭也吃不上。在这样的社会里，毛泽东和整个共产党于是就异想天开，于是就狂妄荒唐，于是就为所欲为，然后开展了轰轰烈烈的大跃进运动，提出了‘人有多大胆，地有多大产’、‘不怕做不到，就怕想不到’等一系列荒唐的口号，把牛皮吹破了天，也没有人敢于站出来指正……”<br />
<br />
“大跃进运动的直接后果就是，饿死了五千万中国老百姓。我想，你的家人和亲戚之中，也有在那个年代被饿死的，或者死于营养不良的并发症。你和我都是“统一思想”的受害者，统一思想害人不浅，谁也逃不了……你说是不是？”<br />
<br />
张处长默然良久，只好点了点头。我再接着说：<br />
<br />
“你所说的坚持马列主义毛泽东思想的问题，我老实告诉你，马克思列宁主义狗屁不通，毛泽东还比不上我一根卵毛。他在理论上的低能弱智，只能贻笑大方。我倒是想写几篇专门的论文，来驳斥马列主义毛泽东思想的荒唐悖乱，并请你想办法弄到《人民日报》上去发表！《人民日报》能发表我这样的论文么？《人民日报》只能搞搞党八股假大空，只能搞搞似是而非的伪命题，只能欺骗和愚弄人民，只能玩弄庸俗下作的东西。《人民日报》应该改名为《愚民日报》才名副其实的。它的自我定位是――‘我是党的一条狗，守在党的大门口。党让咬谁就咬谁，让要几口就几口’的。如果在这样的虚伪、庸俗、恶心的喉舌媒体上刊出我写作的一个字，那是对我的极大侮辱……”<br />
<br />
张处长接口说：“你小子够狂的呀！”<br />
<br />
我接口说：“我没有狂什么呀。我只是愿意说出心中的大实话而已。而不像你们，整天戴着一副假面具，说着言不由衷的假话、套话、鬼话、屁话，做着见不得人的勾当……”<br />
<br />
“我先跟你说说‘毛泽东比不上我一根卵毛’的问题吧！当然，要说‘为达目的不择手段’，要说‘搞阴谋耍手腕’，要说‘厚颜无耻、食言而肥’，要说‘心狠手辣、杀人不眨眼’等手段，我确实不及毛泽东之万分之一。”<br />
<br />
张处长被我勾起了强烈的好奇心，乃说：“那你说说‘毛泽东比不上你一根卵毛’的理由所在？”<br />
<br />
通过激烈争论，看得出，张处长是那种对毛泽东崇拜得五体投地的毛滴虫粉丝。我说的“毛泽东比不上我的一根卵毛”击碎了他心目中的偶像，确实让他既惊讶又好奇，他想看我出洋相。<br />
<br />
我说：“你翻开《毛泽东选集》第一卷第一篇文章――《湖南农民运动考察报告》看看，毛泽东就要去‘小姐少奶奶的牙床上去滚一滚’，这不是一副流氓无赖的嘴脸是什么？”<br />
<br />
“你再翻到毛泽东的《反对自由主义》一文看看，毛泽东把‘会上不说会后乱说’说成是自由主义，把迟到早退说成是自由主义，把不遵守纪律说成是自由主义等等。说明毛泽东对自由主义的基本理论狗屁不通……”<br />
<br />
“你还可以翻到《毛泽东选集》第五卷上，有一篇叫《1953年在最高国务会议上的讲话》，是粗暴谩骂梁漱溟的讲话稿。毛泽东的通篇讲话，简直像流氓耍横，像泼妇骂街。哪里有一点党国领导人的气度、雅量和胸怀？这样的小鸡肚肠，这样的以势压人，这样的蛮不讲理，又如何能以理服人……是因为他被江青指责与新寡的媳妇扒灰，毛泽东有气无处发泄，于是把无名之火撒泼在硕儒梁漱溟的头上……”<br />
<br />
“毛泽东不是说过，‘凡是敌人反对的，我们就要拥护……’按照毛泽东这个逻辑推导一下，我完全可以这样说：‘凡是敌人不吃屎，毛泽东一定要大口大口地吃屎’是不是？”<br />
<br />
这位张处长对于这些问题毫无思想准备，被我问得张口结舌，舌头都收不回来……<br />
<br />
“毛泽东对自然科学一窍不通，只是阴谋权术烂熟于胸。斯诺写的《西行漫记》上，就记载着毛泽东亲口告诉斯诺，他的数学、物理、生物、化学全部是考鸭蛋。他一次国画课上交作业时，他先用一笔画出一个半圆，然后在半圆下面画上一杠，再题上‘半壁出海日’的字样，弄得老师都哭笑不得……”<br />
<br />
“毛泽东还说过‘枪杆子里面出政权’，我告诉你‘共产党的枪杆子里面出的是土匪流氓政权，出的是强权’。毛泽东的‘枪杆子里面出政权’是成王败寇的另一种说法，是山大王逻辑、土匪哲学的另一种表述……”<br />
<br />
张处长接口说：“共产党流血牺牲才打下的江山。付出了那么大的牺牲，当然要维护政权嘛……”<br />
<br />
我接着驳斥他：“你的意思是说，当初打下江山，付出了很大的牺牲，所以这个江山被视为共产党的私有财产？所以要施行专政？这等于向世人宣言――如果谁有本事来抢，请付出同样多的鲜血？这跟‘此山是我开，此树是我在栽，要从此地过，留下买路钱’的山大王逻辑有什么区别？”<br />
<br />
“共产党既然在宪法上写着‘中华人民共和国的一切权利归于人民’的字样，那就承认了主权在民的基本原则。既然承认了主权在民的基本原则，那么‘人民的同意和选择’才是政权合法性的唯一依据。现在坚持的一党专政，既违反了共产党自己所制定的宪法，也违背了人民的意愿，更是对先烈们的公然背叛。这完全是一个非法政权……”<br />
<br />
“我再次声明一下，按照共产党一惯的组织原则和决策体系，只有中共中央的几个政治局常委，才是真正的共产党员。其他的几千万普通党员，仅仅是一组毫无意义的数字。当我说到共产党的时候，请你不要自作多情，不要自认为自己就是共产党的一员。”<br />
<br />
“毛泽东曾经说过‘知识越多越反动’。这说明了什么？说明了毛泽东是一个反文明、反社会、反理性的家伙……”<br />
<br />
“毛泽东曾经说过，‘人类的一切历史都是阶级斗争的历史’，那么过去专制帝王们父子争权、夫妻争权、兄弟争权都是阶级斗争的反映么？这样狗屁不通的混账话，毛泽东不但不以为耻，反以为荣。还要强迫全国人民掀起轰轰烈烈的学毛著的运动呢。要是我说出这样荒唐的胡话，恨不得挖个地洞自己钻进去……”<br />
<br />
“毛泽东发起全国人民学雷锋的运动，因为雷锋拼命学毛著。毛泽东转了一个弯，是发动全国人民学习他自己那些狗屁不通的胡言乱语！阴谋家的手腕，实在让人拍案惊奇！”<br />
<br />
“你可以看到，凡是跟毛泽东跟得紧的人，都没有好下场。从帮他打江山的开国元勋，到他身边的秘书，一个个都不得善终……从刘少奇、彭德怀、贺龙、罗瑞卿、林彪、高岗等等，再到田家英、周小舟、李锐、陈伯达等等。他骂林彪是阴谋家，野心家，怀疑狂，迫害狂，虐待狂等等。而这一切，恰恰说的是他自己。”<br />
<br />
“当年，针对毛泽东引蛇出洞的反右运动，有人指责毛泽东是阴谋。而毛泽东脸不变色心不跳地宣称‘这是阳谋，不是阴谋’。针对毛泽东疯狂迫害知识分子和疯狂杀人运动，曾经有人骂他是暴君、是秦始皇。历朝历代的帝王，哪怕他是如何的残暴昏庸，要是有人这样骂他，他一定气得暴跳如雷……而毛泽东在成都会议上，他听到这样的话之后，不但不暴跳如雷，反而哈哈大笑着说：‘秦始皇算什么！他才坑了460个儒。我杀掉的右派何止46万？我要超过秦始皇一百倍一千倍！你们说得远远不够，还要我自己加以补充……’一个无耻之尤跃然纸上。孟子曰：‘人而无耻，不知其可也’。”<br />
<br />
“毛泽东的荒淫无耻，毛泽东的荒唐悖乱，毛著的胡话连篇，我都懒得数下去了。只要你钻研一下毛泽东的诸多讲话稿和文字，你就会有所认识的……”<br />
<br />
当时，我们有些争论很是激烈，声音很大，搅得宾馆服务员以为客人打架了，乃敲门查看。张处长乃声言曰“跟弟兄们闹着玩，没事没事！”才打发走宾馆服务员。<br />
<br />
经过两个多小时的激烈的争论，击碎了张处长心目中的偶像，他很惊讶，但他心服口不服。并说：“今天的谈话，算是私下交流，你不要记录和发表出去。我们各人坚持各人的观点吧！”<br />
<br />
呵呵！特意从南宁跑来桂林，再赶来全州，迢迢千里，竟然是为了这样的“私下交流”？鬼才信！<br />
<br />
我本来想继续嘲笑他的虚伪。但经过两个多小时的激辩，我已经感到有些累了，加上又到了午餐的时间了，于是被桂林警方的赵警官拉去吃午饭。并说：“嗨！跟他在一起吃饭没意思，让他跟头头们去应酬去吧，我们单独找个地方聊聊……”<br />
<br />
吃饭过程中，赵警官跟我说：“今天的这一通激辩，让张处长领教领教也好。免得他老是跟我们讲蛮话……”<br />
<br />
两个多小时的激辩，涉及的内容很多。我在这篇短文中，也不能全记上。仅仅辑取了少部分。特此煞住。<br />
<br />
补记于民国97年9月2～3日</div>

							
							
							
							
