&nbsp; &nbsp; 　　反而，李鹏日记多次提及温家宝，包括：「凌晨5时许，由中央办公厅统一安排，赵紫阳由中办主任温家宝陪同，我由国务院秘书长罗干陪同，分乘两辆救护车去天安门广场，看望绝食同学。」<br />
&nbsp; &nbsp; 　　换言之，如同罗干陪同李鹏一样，温家宝陪同赵紫阳到天安门只是工作关S。明乎此，也就知道陪赵到天安门构不成温的「罪状」。<br />
&nbsp; &nbsp; 　　李鹏还提及宣布戒严之后，中央高层和地方诸侯纷纷表态。其中温家宝表示：「拥护中央的方针，拥护小平同志的决定，坚守岗位，努力工作，积极完成中央交给中办的各项任务。」<br />
