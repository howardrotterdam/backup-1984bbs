													<h2>中国人的形象为什么那么糟？</h2>
						
						
																											<div id="postmessage_775208" class="t_msgfont">7月31日至8月5日，我到泰国旅游。一家三个人中，只有我是第一次出国。尽管我是家中唯一“崇洋媚外”的“民主派”，但我从来就不稀罕国外旅游。根据我的经验，中国的旅行社组织的旅游，那叫扯淡和受罪。所以，我宁愿一辈子呆在CHINA这个鬼地方，而不想出国折腾别人。<br />
<br />
也因此，我的家庭原计划去新马泰三个地方，由于我的反对，只去了泰国。不过，收获颇大。因为泰国是一个民主国家。她给了我对于民主很直接的感观。尽管泰国目前的民主不尽人意，但是，民主毕竟是民主，民主让泰国的一切（社会的、自然的、人文的）看起来格外真实，而且充满魅力。<br />
<br />
所以，尽管是浮光掠影，但我在泰国感受强烈的地方仍然不少，主要有三：1、任何国家都可以搞民主，不管其在经济、文化上多么落后！如果不搞民主，中国的一切（社会的、自然的、人文的）将不断的继续败坏。2、好国家最重要的标志是公序良俗，而不仅仅是法律制度。所以，国家没有大小、强弱之分，只有好坏之分。这个“好坏之分”关乎社会公德、群体信仰与人性品质。3、中国是世界上最不文明的国家之一。中国人之所以在国外被歧视，是因为中国人仍然没有真正地融入世界。由于大一统的传统，由于没有明晰的产权制度（即“私有制”），所以，中国人暴殄天物，自私自利，并且热衷于“窝里斗”、“鬼打鬼”，即使出国了，这种国民性和劣根性却是明摆着的，所以，我们的海外形象很差。这是中国人自己败坏自己的结果，怪不了别人。<br />
<br />
我们一行16人，主要由几个素不相识的家庭组成。交给旅行社的费用每人1999元人民币，有些还讨价还价砍去了100元。包括往返机票等路费，三个晚上住五星级、两个晚上住四星级酒店的费用，等等。的确很便宜！但就是由于贪便宜，我们惹恼了泰国的导游阿伟。阿伟想让我们增加自费项目，例如“成人秀”（匪夷所思的生殖器功能表演、真枪实弹的性交表演）等。由于主要是家庭团，所以被拒绝。不过，我们还是增加了一些自费项目，例如，看“人妖”表演，吃海鲜大餐，玩海上降落伞，让孩子们游海底世界，等等。每人实际上花费至少四五千人民币。<br />
<br />
应该说，这样的旅程非常值得。问题是，国内旅行社为了拉人头，相互之间在价格起点上恶性竞争，创造出中国独特的旅游模式就是“卖猪仔”，“卖猪仔”是广东话，就是一猪几卖，卖了又卖，在所有被卖的地方重复被宰。最终结果就是中国的旅游团因其极低廉的旅游服务费而被当成“购物团”处理，所以导致了泰国的旅行社不断地向游客们推销他们并不清楚的一些项目，如果游客不答应，就会引起不愉快。但你不应该责怪泰国的旅行社，因为这是中国的旅行社在“卖猪仔”，所以，我们每到一个地方，往往都是“到此一游”，真正的民情、习俗、文化、制度等等这些有价值的东西，都被甩在一边。有人可能会问：“为什么泰国政府不管管啊？”这是典型的“汉思维”、“中国思维”！谁叫你贪便宜了？政府也要管公民个人贪便宜的行为吗？你贪便宜，就得自作自受自作孽。泰国的商品和服务可是货真价实的！你一定要买国内旅行社或泰国导游阿伟这样的劣质服务，你就得自个担当。<br />
<br />
与中国的旅游景点随意宰客、贩卖假冒伪劣不一样的是，泰国各类商品的质量是过硬的。泰国的物品做得很精致，也不贵。我们带回来给同事们品尝的糕点之类，就得到了普遍赞扬，不像中国的同类物品，吃了难受。泰国导游阿伟对我们说，“不要在购物商场中怀疑其商品是否真假，你们那样怀疑别人是不尊重别人的表现，泰国没有假货，卖了假货一律按照原价十倍赔偿；也不要杀价，这里的东西全部都是明码标价的。不过，由于中国人喜欢杀价，所以，泰国近年也开始出现了专供中国人讨价还价的个别购物点。”可怜的中国人！这点小聪明，谁看不出来？人家同情我们，就搞几个“特区”，让你耍点小聪明。聪明的中国人总是逼着他人修改游戏规则。例如，美国的沃尔玛原本是可以“试用期满，不用退货”的，但中国人都是期限用足之后退货，于是美国人不得不推出“防小人”的商业规则。其结果是，聪明反被聪明误。除了旅行社开具的经典线路外，中国人到泰国被逼着逛红灯区、看“成人秀”或参与“人妖”表演，那是必需的。我回来后，听一个女同事说，到泰国就是专门去看黄色的东西的。中国人就这这么一点素质！我们热衷的永远是“两巴”（嘴巴和鸡巴）文化！<br />
<br />
在阿伟的反复蛊惑和请求下，8月3日晚，我们还是带着孩子登上了泰国著名城市芭堤雅的著名景点，“东方公主”号海轮，看“人妖”表演。实际上，我们在曼谷的第二个晚上就看了“人妖”表演。阿伟说，“东方公主”号十分豪华、好玩，是一个不看会后悔一辈子的好地方，因为那里的“人妖”是泰国顶级的。我们看后才知道，这种地方真的不应该带孩子去。“人妖”们的表演非常露骨，很多中国人上去拉下“人妖”的乳罩，伸手抓住“她”们丰满白嫩的乳房一顿乱揉乱捏。这些无比妖艳的“海上仙女”们大跳钢管舞，并且扯下游客的衣服，将其压在身下表演性交。一两千中国人就这样在这里狂呼怪叫、群魔乱舞地集体疯狂。我不觉得“人妖”恶心。恶心的是，居然有这么多的中国人来到这里。几十分钟一批，一批接着一批，每一批都接近两千人，黑压压的上来，然后作鸟兽散。当然，我也恶心自己，一不小心就上了贼船。我听中国导游讲，“东方公主”号是专门用来接待中国游客的。靠！一个小时不到，1000泰铢（约250元人民币）就没了。中国真是“人傻，钱多”！<br />
<br />
然而，就是这么脏、这么傻的中国人居然认为外国人很脏很傻。很多中国游客认为坐厕肮脏，泰国的城市都是坐厕，所以，他们将双脚踩在坐厕上拉屎。据说欧洲人对此忍无可忍，于是他们打出了“中国人，请从马桶上滚下来！”的标语。在泰国，我也看到了类似的警告。在拉玛五世行宫，金柚木宫殿的公厕内，我看到隔间的门内写着：“PLEASE DO NOT CLIMB UP ON THE TOILETS SINCE THEY ARE CHINA……”（咋一看，字面上可翻译为：请勿攀上厕所，因为它们是中国……）开始我很疑惑：怎么厕所是CHINA？这不是污蔑中国人吗？琢磨了半天，我才醒悟到，这的确是在提醒中国人，不过其翻译是：“请勿攀上厕盆，因为它们是瓷器……”然而，这里的CHINA的确可谓一语双关！脚踏CHINA，是自污也是自贱。看到那些踩踏的黑脚印，我边擦干净边想，这一定是中国人干的。为了满足中国人的需要，个别旅游场所也特别配备了蹲厕。不过，这是一种奇怪的蹲厕，其底部伸出地面一二十公分，你的双脚只能很费劲地歧在边沿上。<br />
<br />
中国人将自己的旅程总结为“上车睡觉，下车尿尿”，此外最要紧的是吃。我们的最后一个大餐是阿伟力推的自费项目，在曼谷的KING POWER免税商场三楼吃国际自助餐。的确很丰盛。不过，中国人仍然丑态百出。很多人都“吃到吐”。我看到一位女士的第一盘菜是满满的一大碟海虾，一个人全部扫光。团友说，他看到一位排队等待三文鱼生鱼片的中国年轻女子，在厨师分给她好多块之后仍然指着盆子，示意继续添加，厨师面露难色，同时不无鄙夷，因为她的后面还有很多人在排队，而生鱼片数量有限。所有的人好像都在内心发誓：将花出去的钱吃回来。我们的团友中有一对老夫妇，也是从头吃到尾，我们暗暗称奇：他们怎么突然变得那么精壮？老夫妇边吃边对我们说：你们吃多点。200多块人民币，我们顶多只能吃回50块。难怪阿伟，这位有华人血统的小伙子，曾经在酒店明目张胆地对我们说：“你们吃吧，吃到吐。”这下，还真给他说中了。<br />
<br />
反观泰国人，我们中国人真是自惭形愧！尽管我对这个国家了解十分有限，但是，她一开始就给了我很好的印象。这是一个文明的国度。除了阿伟这样由于素质较低所以口无遮拦的人之外，泰国的人民普遍地是非常友善的。民主国家的人们必然友善。他们见到陌生人，往往会热情地打招呼。泰国在世界上素有“佛教之国”、“微笑之国”等称誉，泰国人看上去无不友善。我们第一晚住的酒店在曼谷的郊区，但是，这里的服务完全是国际化的。酒店的服务人员，包括打扫卫生的，一个个都衣衫整洁、彬彬有礼，总是面带微笑地跟我们打招呼。遇到任何事情，他们很快就做出反应，马上帮忙。你出酒店的门，保安就为你指路。我问阿伟，泰国的社会治安怎么样。他说很好。的确，我们上街宵夜，即使在偏僻的角落，根本就没有看到贼头贼脑的人。曼谷是一个人口密集、车辆拥堵的大都市。但我们看到，无论怎么拥挤，他们的车辆都是一条线的，没有人抢道行驶。千万不要小看这些民情、民风构成的公序良俗！这才是文明的真实成就和象征。<br />
<br />
也许，很多中国游客会说，泰国的民主并不怎么样，泰国的经济也不发达，泰国的文化成就一般，泰国的历史也不深厚，然而，我们无法否认，这是一个开放的、文明的、对自由民主不离不弃的国度。为什么？因为泰国的民情、民风是向善的，实际上，这是人性品质、群体信仰的力量！泰国之所以能够走上民主之路，宗教信仰构筑了牢固的基础。泰国人几乎都有信仰，其中九成以上的泰国人信仰佛教。这样的民情，这样的精神力量，使得泰国社会能够在整体上保持着充足活力。泰国如此，其他民主国家也是这样。他们一律都是热情奔放、崇尚自由、热爱生活而且恪守规则的族群。<br />
<br />
正如托克维尔在《美国的民主》中说的那样：“没有民情的权威就不可能建立自由的权威，而没有信仰也不可能养成民情”，民情“是法律的保障和使自由持久的保证”，“法律只要不以民情为基础，就总要处于不稳定的状态。民情是一个民族的唯一的坚强耐久的力量”。在托克维尔看来，民情（moeurs）一词，其含意与其拉丁文原字mores一样，“它不仅指通常所说的心理习惯方面的东西，而且包括人们拥有的各种见解和社会上流行的不同观点，以及人们的生活习惯所遵循的全部思想”。可见，托克维尔的“民情”，实际上就是“一个民族的整体品德和精神面貌”，也是一个社会群体必要的共识。中国一向以“礼仪之邦”自诩，遗憾的是，中国国内的“民情”从来都是非常糟糕的。中国古代的管子说得很好：“礼义廉耻，国之四维，四维不张，国乃灭亡。”然而，这只能算是一种“片面的深刻”。因为中国文化传统中，一直结不出信仰、自由、法律、规则的果实，所以，中国文化患有“偏头痛”（胡星斗教授的说法）。<br />
<br />
泰国是君主立宪制国家。泰国的国王深受泰国人民的尊敬和爱戴。然而，这决不是盲目的个人崇拜。其中的重要原因是，泰国的王室并非高高在上，她与泰国人民从来都是近距离接触的亲密关系。泰国的街道、公路上到处都悬挂着国王和王后的照片，高贵而优雅的王室就是泰国的象征。然而，没有人因为这个而神化王室。人们能够自由地信仰自己的神灵。并且，王室对于任何国民都没有多少秘密可言。到泰国，必不可少的旅游项目不是观看“人妖”表演，而是浏览泰国历朝历代的皇宫。泰国的王室对这个国家的贡献非常之大！例如，泰国著名的第三世王兰甘亨大帝，在13世纪创制了泰国今天的文字。泰国现在的第九世王普密蓬&#8226;阿杜德国王，尽管在政治上保持中立，但他用心经营王室财产，以资助整个泰国社会，尤其是贫困阶层。例如，国王亲自研究开发新的农作物品种。<br />
<br />
有一个很普遍的说法是：“中国人素质低，不适合搞民主。”真的吗？显然是错误的。我们不是历来自诩“历史悠久”、“文化优秀”吗？如果较真的话，泰国人跟中国的“少数民族”中的傣族人应该是有血脉渊源关系的。傣族语言有六七成和泰国的几乎一模一样。很多贫穷落后地区的广西壮族人也能大致听懂泰国语言。泰国基本上是一个农业国家。然而，她为什么可以实行民主制度？原因很多，但我们可以一言以蔽之：民主是当今世界的通行规则。不民主，意味着反文明。如此而已。“大国崛起”不过是特权自编自导的痴人说梦。唐代的杜光庭在解读《老子》一书时说道：“理国亦以柔和为上，不以强大为能。弃柔任强，丧身败国矣。”真正的强大，从来不是国家的，而只会来自国民的文明自信。中国需要做的事情并不艰难困苦，我们只需要融入这个文明的世界。</div>

							
							
							
							
