&nbsp;&nbsp;<br />
中共禁香港苹果日报大陆采访的一个原因（图） <br />
&nbsp;&nbsp;<br />
倪丑 <br />
&nbsp;&nbsp;<br />
【人民报消息】上海世博5月1日揭幕了，香港立法委员代表团准备5月8日到10日去参观。香港苹果日报希望派记者随团采访。但早早就递上申请记者采访证，却至今没有核发，也没说明拒发原因。打电话询问，除了「打太极拳」，还是「打太极拳」，最后是泥牛入海无消息。<br />
8日这天朋友送过来一条新闻，来源香港苹果日报，让人不能不大笑，这大概是不发记者证的原因之一。说句良心话，这可不是香港苹果日报在恶搞中共，只不过是用心综合了一些中共官方新闻的公开花絮罢了，就好象大陆摄影记者拍摄到数不清的江泽民两会丑态，但发稿时决不敢采用；而外媒发稿时专门挑江的那些恶心人的图片出售。那当然了，否则谁买啊。<br />
<br />
这些花絮一共有9条，其实要是有心去搜集，900条也不止。不过这9条已经让党跳起来了，说是政治事件，让涉「案」记者、编辑甚至总编辑，轻则被处分，重者卷铺盖卷儿滚蛋。<br />
<br />
<br />
<br />
 <br />
2001年，江为九运会点火炬。 <br />
 <br />
2001年纽约世贸中心双子塔被撞击起火！<br />
 <br />
<br />
<br />
其中被定为最严重「政治事件」，在江泽民当政的2001年，《北京晨报》头版刊出美国世贸大厦「 911」被恐怖份子袭击起火的图片旁边，配的是江泽民为九运会点火炬的图片。<br />
<br />
严格的说，这图片搭配的非常合适，因为中共一直视世界恐怖份子为最亲密朋友，而且明里暗里支援他们军火和钞票。<br />
<br />
1998年，江泽民头上的邓婆婆刚死一年，江喜极而泣，眼泪还没擦干，其装样儿去「视察长江抗洪」时，《九江日报》报导称「江怪民」前来视察，引起江大怒。<br />
<br />
2004年10月1日，《江西广播电视报》国庆版大标题写道：「庆祝中华民国成立 55周年」！该报因此被停刊整顿。<br />
<br />
2006年，江西某报把「没有共产党就没有新中国」第一个「没」字漏掉，成了「有共产党就没有新中国」，与2004年那个「庆祝中华民国成立 55周年」一意贯通。结果被定性为「犯重大政治错误」。<br />
<br />
1997年香港回归，东北某报纸将《香港明天更美好》排成《香港明天更无好》，香港人因此受到惊吓。同年，武汉晚报将李鹏印成「国务院总经理」、人民公安报将时任国家主席杨尚昆印成「杨尚屁」等。<br />
<br />
2006年 11月 21日，新华社报道胡锦涛会见外宾，标题把胡锦涛写成「胡锦铸」。<br />
<br />
1993年北京申办奥运，有报纸以《北京申办 20000年奥运会》为题报道，读者大哗：中共还想活这么久？疯啦！<br />
<br />
香港苹果日报报导时，未加评论，仅把这9条摆出来而已，即使这样，香港苹果日报也上了中共的黑名单，恐怕这个报社的记者真要到「有新中国没共产党」时才能进入大陆采访。好在等不了多久了。贵在坚持。△<br />
<br />
