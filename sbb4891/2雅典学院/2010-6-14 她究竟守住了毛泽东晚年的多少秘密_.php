																											<div id="postmessage_681963" class="t_msgfont">一直以来，对于张玉凤，有着很多的文字追踪于她。有党史研究的，有纪实报告的，还有民间演绎天上地下信口开河的文字。对于后者，张玉凤沉默了太多不该承担的附会。张玉凤的善良、付出、守纪，是值得我们报以极大尊敬的一个人。<br />
&nbsp; &nbsp; 有关她最简单的介绍文字是这样的：张玉凤，女，1944年1月27日出生，黑龙江省牡丹江市人。1962-1970年在毛泽东乘坐的专列上工作，1970-1976年为毛泽东身边工作人员，1974-1976年任毛泽东的机要秘书。<br />
&nbsp; &nbsp; 1976年后张玉凤去了哪里，干了什么虽然是人们关注的话题之一，但是，已不太重要了。重要的是张玉凤走近毛泽东、守护毛泽东，到最终送走毛泽东，她一直是一个最知情毛泽东的人。1962年至1970年，从“七千人大会”后到中共九届二中全会(庐山会议)，这中间经历了很多的重大历史事件，其中最主要的是，像毛泽东与刘少奇的冲突、发动文化大革命、与林彪的冲突等等；这个时候的张玉凤，还只是一个旁观者。但作为毛泽东专列上的服务员，她一定看到过很多很多。1970年到1974年，她作为毛泽东身边的“工作人员”已经不是走近而是走进了中南海，就在毛泽东身边工作了。她看到了“后台”的林彪事件发生全过程，感受过毛泽东因此遭受重创的一切，也见证了毛泽东随后处理此事所付出的全部努力。而1974年至1976年9月9日毛泽东临终，张玉凤已经是毛泽东的机要秘书了。这期间，中国政治经历了邓小平复出、四届人大召开、江青集团力量坐大和式微、毛远新出任毛与政治局的联络员、周恩来病重、所谓的“反击右倾翻案风”、总理人选安排、大量受屈干部的政策落实、军队干部对调、再次扳倒邓小平、天安门追悼周恩来活动、唐山大地震、毛泽东去世、毛去世后各派力量的角力、粉碎“四人帮”等等重大事件。<br />
&nbsp; &nbsp; 作为机要秘书，张玉凤想不看到什么都难。更何况，毛泽东晚年，他离不开张玉风，毕竟，从1962年起，这位如花年龄的女性就在毛泽东的专列上服务了。可以说，毛泽东想到什么，张玉凤就能敏感地知道一二。毛泽东晚年发音不清楚，但只要他动动嘴巴，发出哪怕几个不连贯的音节，张玉凤基本上就能解读出来。曾经发生过毛泽东与张玉凤冲突后，毛泽东将张玉凤驱逐出去的事。但张玉凤可以走，毛泽东却离不开她，没有多长时间，毛泽东先自认输了，让汪东兴尽快将张玉凤找回来，并说下了很动情的话：我也这么大年纪了，脾气是有的，难道你的脾气比我还大?也还真是张飞的后代呀!<br />
&nbsp; &nbsp; 不能说因为张玉凤对毛泽东的了解太深就真敢与毛泽东叫板对撞，但话又说回来了，毛泽东的晚年脾气大不同于身体健康时期，实在是不大好侍候的。张玉凤能一直随身于左右，帮毛处理好一些别人未见得能代替她处理得好的事――这也是中央为什么做出让张玉凤任毛泽东“机要秘书”的重大安排及其理由。机要秘书是做什么的呢?简单言之，就是毛泽东有关的“机密”、“要件”的转递、落实、存档。在毛泽东生命的最后两年里，张玉凤成了各派关注的对象，连江青这种什么人都看不起的角色，也不得不小心地求助于张玉凤，尽量地巴结“小张”，包括用一些女人之间的小恩惠小手段。当然这也无可厚非。但由此可以见出张玉凤在当时政治格局中的份量。曾写过一篇《毛远新“贪污”过毛泽东的谈话吗》，结论是不曾有过“误传圣旨”的过错，这中间就有着张玉凤的工作勤恳敬业等因由，因为是她对毛泽东任何谈话语音和文字文本的执着追索，没有给他人留下其他发挥的空间。<br />
&nbsp; &nbsp; 张玉凤没有主动做过影响毛泽东决策的任何“进言”，这是一种操守。她所做过的一件让她后来还感到害怕的事就是在长沙“批走后门”事件的代毛圈阅。其它，张玉凤守住了本色，经受了时间的考验。但不参与，并不代表不知情。有关中国为什么发生文化大革命、刘少奇之死留下的蹊跷、毛泽东在滴水洞给江青的信之有无、林彪事件、中央政治局的人事安排、王洪文的升迁沉浮、康生其人及其状告江青、毛泽东眼里的邓小平、毛泽东与江青的关系、毛远新在毛泽东的住室里谈话的全部、毛泽东为什么选择华国锋的政治和战略考量、毛泽东为什么不参加周恩来的追悼会、天安门悼周时毛泽东在病室里的所有情形等等。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 图：张玉凤与晚年毛泽东<br />
&nbsp; &nbsp; 毛泽东去世后，当时中南海的大管家，也是粉碎“四人帮”后迅速成为“五大领袖”的汪东兴曾找张玉凤索要过很多东西。而此前，江青也在一直打着毛泽东晚年谈话记录的主意。在江青向张玉凤索取保险柜钥匙时，张玉凤却说：“主席留下的一切，都是党和国家的财富，若要清理必须经华主席批准”。张玉凤坚持自己的职守，做了她的力量和能力范围里能做的一些事情，对历史档案有了一定的担当。因为张玉凤是毛泽东生命最后两年里的机要秘书，她处理和见证了当时最核心的机密档案。在粉碎“四人帮”后很长的一段时间里，人们对毛泽东为什么不参加他几十年的老战友周恩来的追悼会一直有着深重的怀疑。海外报刊就此事更有多样的解读。而且这种解读也被中央文献出版的《周恩来年谱》、《周恩来传》、《毛泽东传》的写作者之一、原中央文献研究室室务委员、周恩来生平研究小组组长高文谦所证实确有其事。比如说毛泽东对于天安门百万群众自发地悼念周总恩来一事，毛泽东在送达的《动态》上的批示就是“假悼念，真复辟”六个字。而且毛泽东也真的没有参加追悼会。据传，毛泽东在1976年1月12日还讲过不参加的理由：“为什么我要参加总理的追悼会?我还有不参加的权力嘛!(略)”对此情况，当时的中央有关部门请张玉凤这个历史见证人出来写文章作一些解释，以证明毛泽东确实是病重不能走动了，因之没有去参加周的追悼会。<br />
&nbsp; &nbsp; 张玉凤这样做了。她写了毛泽东当时患严重疾病的情况，证明毛泽东的腿当时确实“走不动”了，因而只在送审的有关周恩来追悼会参与人员的报告上“画了一个圈”。但是，我们从张玉凤的文字里，还是可以看出她对毛泽东没有出席周总理追悼会的“看法”与情绪。虽然她的文字已经十分委婉了：<br />
