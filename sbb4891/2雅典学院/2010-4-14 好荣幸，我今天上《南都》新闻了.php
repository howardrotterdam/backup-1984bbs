<br />
网友写信申请信息公开 公安部：请咨询有关部门<br />
广州一大学生申请公开全国警方收取的行政罚金总额<br />
类别:国内新闻&nbsp; &nbsp;浏览量: 1167&nbsp; &nbsp;版次:AA21&nbsp; &nbsp;版名:网眼&nbsp; &nbsp;稿源:南方都市报&nbsp; &nbsp;2010-04-14<br />
作者:张东锋 原创&nbsp; &nbsp;手机看新闻 全国订报编辑此文<br />
摘要：3月初，网友“YODA大湿”在自己的博客上贴了一张寄往公安部的信封和一份《公安部政府信息公开申请表》的图片，申请公开“2009年全国公安部门所收取的行政处罚金的总额”。4月初，他收到了《公安部政府信息公开申请答复书　2010年003号》，告知他因为所申请内容不属于公安部掌握的范围，所以依据《政府信息公开条例》第二十一条规定，建议他向“有关部门”咨询。<br />
&nbsp; &nbsp; 3月初，网友“YODA大湿”在自己的博客上贴了一张寄往公安部的信封和一份《公安部政府信息公开申请表》的图片，申请公开“2009年全国公安部门所收取的行政处罚金的总额”。<br />
<br />
&nbsp; &nbsp; 4月初，他收到了《公安部政府信息公开申请答复书　2010年003号》，告知他因为所申请内容不属于公安部掌握的范围，所以依据《政府信息公开条例》第二十一条规定，建议他向“有关部门”咨询。<br />
<br />
&nbsp; &nbsp; “YODA大湿”是广州一所大学一年级学生王璋照，他接受南都记者采访时说自己是想“看看政府部门的诚意”，还真收到了回复，但这个“有关部门”让他有点失望。<br />
<br />
&nbsp; &nbsp; 收件地址：公安部<br />
<br />
&nbsp; &nbsp; 对王璋照来说，这次政府信息公开申请多少有点行为艺术的味道。他对南都记者说，之所以向公安部申请信息公开，其实并没有特别的用意，只是在《信息公开条例》颁布了之后，“看看政府部门的诚意”，而申请信息公开的过程，也在博客上作了描述。<br />
<br />
&nbsp; &nbsp; 3月4日，他发出博客《收件地址：公安部》，贴出信封和申请表的扫描件，申请公开的信息内容是“2009年全国公安部门所收取的行政处罚金的总额”，至于对“所需信息用途”的描述，则更像是申请信息公开的理由：“《信息公开条例》规定政府行政机关应公开需要社会公众广泛知晓的信息”。<br />
<br />
&nbsp; &nbsp; 以普通公民身份向公安部这样的部委申请信息公开，王璋照不知道最终会有什么样的结果。他当时在博文中写道：“半小时前刚刚把这封信投入信箱，不知道最后结果会怎样？”“永远得不到回复？被当做刺探国家机密的被抓起来？或者是真的能等到一封可以给出我所需要的信息的回信？”<br />
<br />
&nbsp; &nbsp; 收到了公安部的回信<br />
<br />
&nbsp; &nbsp; “忐忑不安地期盼了好久，终于收到了公安部方面的电话。语气挺友好的，比我想像中的要好。”4月11日，王璋照发表博文《收到了公安部的回信》。<br />
<br />
&nbsp; &nbsp; 公安部在回复中表示，“经查，你申请获取的政府信息不属于本机关掌握的范围，根据《信息公开条例》第二十一条规定，建议你向‘有关部门’咨询”，署名是公安部政府信息公开办公室。答复的最后，还附了一行解释：根据“行政处罚法”的有关规定，行政处罚实行“罚款与收缴相分离”的原则，公安机关不掌握相关信息。<br />
<br />
&nbsp; &nbsp; 对此，王璋照难掩失望，“仔细地看了几遍，很失望。”与网民关注的焦点不同，让他感到失望的是“有关部门”这个字眼儿，“有关部门是什么部门？”<br />
<br />
&nbsp; &nbsp; 在他看来，同样的《信息公开条例》第二十一条，其第三项还规定，“依法不属于本行政机关公开或者该政府信息不存在的，应当告知申请人，对能够确定该政府信息的公开机关的，应当告知申请人该行政机关的名称、联系方式”<br />
<br />
&nbsp; &nbsp; 公安部去年收到申请26件<br />
<br />
&nbsp; &nbsp; 根据《公安部2009年政府信息公开工作年度报告》，2009年，公安部共受理政府信息公开申请26件，全部按时答复，答复率100%。答复意见中，“同意公开”的9件，“同意部分公开”的2件，依法不予公开相关政府信息的15件。在依法不予公开相关政府信息的答复中，“不属于本部门公开政府信息”的6件，“政府信息不存在”的1件，因其他原因不予公开政府信息的8件。<br />
