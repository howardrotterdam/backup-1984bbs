													<h2>回应北京棋迷关于皮诺切特式威权主义不可行的看法</h2>
						
						
																											<div id="postmessage_557083" class="t_msgfont">对我的观点总结一下<br />
首先，TG绝不可能自行改良。因为现在的TG，是一个比组织苏联更加严密的寡头共治集团，胡温不过是这个集团的代表而已。实权掌控在人大九常委的手中。所有大的政策要由他们一同来决断。自从苏联巨变以后，他们肯定加强了防备，这种情况下就算领导人里面出了叛徒，其他人也会联手干掉他，让他做赵紫阳二世。所以说棋迷的笑话是对的，就算弗里德曼转世到中国当了主席，他要么收起他的改革计划当胡锦涛二世，要么被其他几大常委送去陪赵紫阳。<br />
那么九常委不可能自觉交权么？绝对不可能，因为吃进嘴里的蛋糕是不会有几个人会自愿吐出来的。而寡头共治的体制又决定了就算有几个理想主义者想改变现状，其他人也不会放过他。<br />
至于刘晓波们的&quot;曲线救国‘根本就是无稽之谈。你要知道，TG的领导层绝非无能之辈，这点小伎俩他们是很清楚的。他们不是不明白孰是孰非，但是他们被利益与制度的枷锁套牢，不可能有所改变了。<br />
那么TG彻底垮台有没有可能呢？完全有可能。共产主义信仰已经彻底破产，TG现在的执政合法性全部维系在经济之上，中国人“有饭吃，不扯淡”的民族性决定了经济不崩溃，大部分的中国人是不会反抗的。所以经济不出问题，TG就别想倒台。<br />
但是，从目前的经济形势来看经济崩溃不过是个时间问题。（这个问题太大，另文讨论）。经济崩溃后，TG必然完蛋。我们该怎么办呢？<br />
一种是走和平“革命”既天鹅绒式革命路线。这个是非常扯淡的。且不说目前民主人士中缺乏像哈维尔，曼德拉等具有广泛号召力的领袖。也不说民主人士普遍存在的软弱性。单就他们上台后的表现来看，他们是不会对社会现状有多大改善的。因为中国的经济是烂在根子里的。需要对整个社会经济体系进行大规模的改革。这将侵犯到一大批既得利益分子的利益，他们必然会群起反抗。这就需要用强硬手段来推行改革，而“宽容，妥协”的刘晓波们无疑是做不到这一点的。<br />
那么，我们就只剩下利用民粹，草根民主来进行大规模的底层人民暴动的方式来推翻TG,建立新国家一条道路。从薄熙来在重庆的“打黑唱红”运动成果来看，至少在某些地方，民粹还是爆发出了应有的力量的。利用民粹，就连薄熙来这种臭狗屎都能取得如此“成就’我们难道不可以吗？<br />
但是从”唱红打黑“运动中我们也看到了民粹的巨大缺陷，那就是隐藏在民族性中对”均贫富“的追求理念。这就决定了如果不加以控制，民粹必然失控甚至导致共产主义的彻底复辟。而且，民粹需要一个强有力的领袖来调动。综合以上，我们需要一个强力且反对共产主义的领袖来利用民粹的力量。这就又回到了皮诺切特式的强权政治模式。<br />
北京棋迷的”现在何止全中国, 全世界人民都对独裁极权深恶痛绝, 你却要光明正大的让独裁极权为自由市场保价护航, 这就像袁世凯要在中国搞君主立宪为宪政保驾护航一样傻逼.“说法，我们是不能判定其对错的。因为这事关民意，而民意是实实在在摆在那里，不是我们几个人就能讨论出来的。单就我个人所知，我所知道的不少人都认为”中国这么大， 民主的话就乱了“。但主流民意对此事态度究竟如何，没有大规模的民意调查是不会有确切结果的。所以我认为，在这个问题上讨论，是没有意义的、我们应该搁置这一争议。<br />
但是棋迷的说法确实有可取之处，这就告诉我们，直截了当的搞威权统治毕竟是有风险的。很有可能成为袁世凯第二，身败名裂。<br />
我认为，这个问题其实不难解决。袁世凯的愚蠢之处，就是过于看重名位，导致身败名裂。其实名位固然重要，但比起身败名裂来孰轻孰重，大家心里还是有数的。<br />
我以为，威权主义和民主宪政不是不共戴天的（表面上）民主制度可以搞，但是要留出后门，确保民意不会干扰到实际的威权统治下的全面改革。这是个技术性问题，我无法做出明确的阐述。简单提几个我的设想：<br />
一：宪法中规定，总统只允许有一次任期，长十二年（既堵住了某些人的嘴，预防独裁者赖着不走，同时十二年时间也足够完成改革）二：禁止共产党人和左派参选议员（防止左派进来捣乱），剥夺共产党官员及其直系亲属，国企老总的一切政治权利（其实他们是要被干掉的）<br />
三：总统拥有全权军事权力，且军费开支议会不得干涉（留一手后着）<br />
四：对议员实行资产限制，个人拥有资产超过******元或低于*****元者不得担任议员职务（防止极左极右混进来）<br />
五：对于对前政府和前国企官员的清算问题，议会不得干涉（我主张对付共产党的手段，能让刘晓波听了后昏过去。可不能让他们坏事啊）<br />
六：总统拥有一票否决权（这个是我想出来的最狠主意）<br />
