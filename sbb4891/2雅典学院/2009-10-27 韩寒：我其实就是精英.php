													<h2>韩寒：我其实就是精英</h2>
						
						
																											<div id="postmessage_302182" class="t_msgfont"><img src="http://img617.ph.126.net/g8aYLc_0HqCbMnP8pS_ZCQ==/1911778041820533345.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
 韩寒：我其实就是精英 - L尚周 - 风尚周报<br />
<br />
文/吴小娜 图/周云哲 来源：风尚周报<br />
<br />
本文节选自《风尚周报》109期，全文详情见杂志<br />
<br />
《风尚周报》：回顾过去，你付出过什么代价？<br />
<br />
韩寒：当很多人渣都过得很好的时候，你会发现，我为什么连一套房子都买不起。我会想为什么，我是全中国最好的赛车手，畅销书作家，光这两个行当在别的国家都是千万千万地那么挣的，为什么一套房子不光说全款买不起，我连首付都付不起。我是怎么过的，是不是有时候太大方了，因为我周围很多朋友买房子不想为银行贷款，他们都问我借，那我就借给你了，可是从来没有人想过我。很多事情对他们来说是一些噩梦，我经常在拯救他们的噩梦，可是到头来，谁来拯救我的噩梦。这些事，有的时候想一想，心里会很失落。但转念想想，真的不能拿那些人渣去比自己的生活，他们一天到晚去骗风头，我做的都是自己喜欢的事情，而且最关键的是我衣食无忧，我的家人我都把他们养活得很好，我其实很满意。<br />
<br />
 <br />
<br />
《风尚周报》：你还有什么梦想？<br />
<br />
韩寒：不能告诉你。我往往是实现了才告诉大家这是我的梦想，要不很丢人的。我是属于那种看到了就一定一定要得到的人。<br />
<br />
 <br />
<br />
《风尚周报》：最近对自己有什么发现或认识？<br />
<br />
韩寒：这也不能告诉你。你可能会经常发现自己：咦，我居然是这种人。所以不能告诉你。<br />
<br />
 <br />
<br />
《风尚周报》：你的家庭传统吗？<br />
<br />
韩寒：传统，我妈妈爱上的是一个男的，我也特别传统，我只喜欢异性。我不认为一个忠于自己想法的人叫做叛逆，而且没对社会造成危害。<br />
<br />
 <br />
<br />
《风尚周报》：你是一个完美主义者吗？你会为了得到完美的结果患得患失左右摇摆吗？<br />
<br />
韩寒：算是，但我可以接受过程不那么完美。是人类都会（患得患失），可能赵忠祥都会。这主要是处理素质的问题，有的人的中央处理器可能稍微慢点。我也会犹豫，但我是一个车手，犹豫得比较快而已。<br />
<br />
 <br />
<br />
《风尚周报》：在感情方面呢，如果情感和利益揪扯在一起呢？<br />
<br />
韩寒：情感当然是最难的。和一个人在一起，你说爱情和快乐重要呢，还是名和利重要，当然理想状态是都有。那假设，不能都有呢？选感情的，往往是最可悲的，可悲的原因在于感情它有可能会改变的，但名和利是永远可以给你带来快乐的。我觉得有时候感情，一时冲动会让你产生放弃一些名利的想法，但冲动过去以后，大部分人还是会重新投入名和利的怀抱，我也不知道这是一种可悲还是什么，这的确是没有办法。<br />
<br />
 <br />
<br />
《风尚周报》：那么现在你的生活里，哪个更重要？<br />
<br />
韩寒：那肯定还是感情，这里面不仅仅是爱情。感情这个东西带来的影响是你不能预估的，它对一个人的状态跟心情的影响是没法控制的，不是工作中的那些东西，可以控制。<br />
<br />
 <br />
<br />
 <br />
<br />
《风尚周报》：近来贾平凹的《废都》解禁，你认为现在可以说是写作的好时代吗？<br />
<br />
韩寒：他那个解不解禁都没大所谓，因为在这个年代里，有太多比《废都》要high的东西，而且我并不认为《废都》是因为超前而被禁了的。<br />
<br />
 <br />
<br />
《风尚周报》：如果你有权力，你会把易中天送到哪个朝代去种田？<br />
<br />
韩寒：把他送到当代去种田就可以了，让他感受一下这田种着种着又没了的感觉。在古代，虽然苛捐杂税很多，但这田还是田，你可以种一辈子，但在当代是不行的，种着这土地就被收去了，所以让他感受一下种田都种得居无定所的感觉。<br />
<br />
 <br />
<br />
《风尚周报》：如果花木兰、祝英台、李宇春在你身边，你会首先发现谁是女儿身？<br />
<br />
韩寒：许仙，我加进去的。说实话，那三个在我眼中都是女人，因为我会问她们名字啊，一问就知道是个女人嘛。<br />
<br />
 <br />
<br />
《风尚周报》：有人说你和时寒冰是当代的鲁迅，你怎么看？<br />
<br />
韩寒：这两名字都够寒的，我特别不好意思。我觉得这是完全不同的两个年代，鲁迅那个年代是中国最开放的年代，而且鲁迅这个人并不是教科书里那样的，在很多人都没弄明白鲁迅是个什么东西的时候，他根本就没法去定义谁是又一个鲁迅。事实上不可能的，鲁迅长那么难看，我怎么能是第二个鲁迅呢？<br />
<br />
 <br />
<br />
《风尚周报》：你是精英还是愤青？<br />
<br />
韩寒：你这是审我还是骂我？虽然这个词在中国不大好听，但我其实就是精英啊。主要是很多人都不好意思承认，那我只能带头先承认了。</div>

							
							
							
							
