<br />
曾任耶鲁大学校长的小贝诺&#8226;施密德特，日前在耶鲁大学学报上公开撰文批判中国大学，引起了美国教育界人士对中国大学的激烈争论。<br />
<br />
对中国大学近年来久盛不衰的“做大做强”之风， 施密德特说：“他们以为社会对出类拔萃的要求只是多：课程多，老师多，学生多，校舍多”。<br />
<br />
对于通过中国政府或下属机构“排名”、让中国知名大学跻身“世界百强”的做法，施密德特引用基尔克加德的话说，它们在做“自己屋子里的君主”。<br />
<br />
中国大学近来连续发生师生“血拼”事件，施密德特认为这是大学教育的失败，因为“大学教育解放了人的个性，培养了人的独立精神，它也同时增强了人的集体主义精神，使人更乐意与他人合作，更易于与他人心息相通”，“这种精神应该贯穿于学生之间，师生之间”。<br />
<br />
中国大学日益严重的“官本位”体制，施密德特也深感担忧，他痛心地说：“宙斯已被赶出天国，权力主宰一切”。<br />
<br />
施密德特为此嘲笑中国大学“失去了重点，失去了方向，失去了一贯保持的传统”，“课程价值流失，效率低，浪费大”。<br />
<br />
由于当前经融危机引发的一系列困难，导致大学生就业难。施密德特对此说，“作为教育要为社会服务的最早倡议者，我要说，我们千万不能忘记大学的学院教育不是为了求职，而是为了生活”。他说大学应该“坚持青年必须用文明人的好奇心去接受知识，根本无需回答它是否对公共事业有用，是否切合实际，是否具备社会价值等”，反之大学教育就会偏离“对知识的忠诚”。<br />
<br />
对中国大学的考试作弊、论文抄袭、科研造假等学术腐败，施密德特提出了另一种观察问题的眼光，他说“经验告诉我们，如果政权是腐败的，那么政府部门、社会机构同样会骇人听闻的腐败”。<br />
<br />
施密德特认为中国大学不存在真正的学术自由，他说中国大学“对政治的适应，对某些人利益的迎合，损害了大学对智力和真理的追求”。他提出“大学似乎是孕育自由思想并能最终自由表达思想的最糟糕同时又是最理想的场所”，因此，大学“必须充满历史感”，“必须尊重进化的思想”，“同时，它倾向于把智慧，甚至特别的真理当作一种过程及一种倾向，而不当作供奉于密室、与现实正在发生的难题完全隔绝的一种实体”。</div>

							
							
							
							
