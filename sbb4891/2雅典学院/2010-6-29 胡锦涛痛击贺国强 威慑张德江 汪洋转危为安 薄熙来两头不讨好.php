打黑，原本是个治安问题，一旦被某个政客形成运动，就有其不可告人的政治目的。薄熙来的重庆打黑，从一开始就运用大量现代传媒技术造势，不讲法律程序公正。因此，运动之初，就含有薄书记不可告人的政治动机。 (博讯 boxun.com)<br />
<br />
薄熙来的打黑运动，有如下几个特点：<br />
一、从来不在自己的势力地盘，如辽宁、大连，打黑<br />
二、只在他人的政治地盘――重庆，打黑<br />
三、彻底砸烂重庆原有公、检、法人事系统<br />
四、将打黑形成运动，在重庆官场搞人人过关，形成薄书记的红色恐怖<br />
<br />
什么是政权？以前的文章讲过，政权就是镇压权。简单讲，就中央而言，政权就是军权。谁掌握军权，谁就掌握国家的政权。就地方而言，政权就是公检法。谁掌握公检法，尤其是公安，谁就享有地方政权。什么开办娱乐中心、黄赌毒，暴力拆迁，官商结合盗卖土地资源，哪样不需要公安在背后支撑，所谓的黑社会势力，只不过是公安在前台的打手，是公安系统的第三产业，是协助政权完成政府职能的一个半官方的部门。因此，各地公安无不大发横财。但是巨额财富在手，也需要政治保护，才能可持续地发财下去。因此投靠谁，站在谁的队伍了，把宝压在哪一方，是中共官场上一门颇为讲究的学问。(boxun)<br />
<br />
前重庆公安局副局长、司法局长文强，是重庆公检法的实力派人物，因此也是各政治派系在重庆笼络的重点对象。笼络住了，大家一起发财；笼络不住，则是必先锄之。显然，在江系太子党大战团派的中央政治权斗格局中，文强把宝压在了团派汪洋身上。加上汪洋对下属并不苛刻，两人相处也颇为和睦。即使是汪洋十七大肩负胡锦涛政治重托，前往广东解放思想，文强也是鼎立相助，资助了不少钱财，疏通政治生态圈中的人与事。这就是文强叫嚣“别想通过审问从我口中获得更多的东西”的道理。同时也是向汪洋、胡锦涛表态，决不牵连自己的老上级、老领导。<br />
<br />
然而薄熙来一到重庆，就首先考验文强的政治态度，先后在几个不同场合，明里暗里敲打文强，重庆公安第三产业的收入都到哪里去了，言外之意是赶快拿钱来。文强自持有团派重臣汪洋作厚盾，并不太理会薄熙来的威胁，所以才被薄熙来冠之以重庆黑社会保护伞的称号。(boxun.com)<br />
<br />
从薄熙来的在辽宁与重庆的所做所为，重庆高调打黑运动的政治动机与目的，无非是以下几点：<br />
<br />
一、汪洋在广东发起的解放思想，就是要从党章与宪法中废除对江泽民三个代表的个人迷信与崇拜，来树立对胡锦涛科学发展观的个人迷信与崇拜。因此江系势力，除了最恨胡锦涛，其次就是汪洋，因为这是要动江泽民的政治命根子。借发起重庆打黑政治运动，可以丑化重庆前任书记汪洋的无能，甚至是暗地指控汪洋包庇纵容黑社会。薄熙来自以为此举可以亮明自己的政治立场，在胡江政治斗争中，是站在江系太子党一边，在十八大之前可以获得太子党的政治支持。<br />
<br />
二、丑化、指控汪洋包庇重庆黑社会，可以让江系上海帮与太子党政治势力，在十八大上有足够的理由阻止汪洋进政治局常委会，论功行赏，空下的常委位子，自然是非他薄书记莫属。<br />
<br />
三、砸烂重庆原有公检法人事系统，正是建立自己公检法人事系统的好机会，这样薄书记就可以牢牢掌握重庆地方政权，建立自己在重庆的黑社会，可以日后财源茂盛。<br />
<br />
四、想进政治局常委会，需要政绩，老百姓不明就理，只看见打黑，看不见自己的政治动机与目的，当然的一片叫好声。以后就算胡锦涛不高兴，有民意在，也奈何不了自己的政治地位。(boxun.com)<br />
<br />
面对薄熙来以上的反革命两手，胡锦涛立即部署革命的两手：<br />
<br />
一、薄熙来的重庆打黑一开始，江系在政治局的势力立刻要问责汪洋，指责重庆黑社会是在汪洋主政时期坐大。然而胡锦涛并没有采取为汪洋辩护的被动消极防御策略，而是实施围魏救赵积极主动战略，揪住政治局常委、中纪委书记贺国强不放，声称重庆黑社会的兴起，始于十年前贺国强主政重庆的1999年―2002年时期。言外之意，当年中组部是如何考核重用贺国强的。贺国强2002年上调政治局委员、书记处书记、中组部长，考核者不是别人，正是江系太子党党首曾庆红。胡锦涛的围魏救赵战略，立刻扭转不利的势态，将火引向了江系态子党党首。<br />
<br />
二、胡锦涛没有庇护汪洋，而是坚持捆绑批评贺国强与汪洋，要求两人一起在政治局生活会议上作检讨。江系势力想就此拿下汪洋的政治局委员资格，那好，将贺国强的政治局常委资格一起拿下好了！这显然是江系曾庆红太子党势力无法承受的。<br />
<br />
为此，中央政治局常委会，在顾全大局保持稳定的宗旨下，对贺国强、汪洋的问题作出四点决定：<br />
1、贺国强同志、汪洋同志，在重庆任职期间，就地区黑恶势力问题的检查、认识是严肃的、认真的；<br />
