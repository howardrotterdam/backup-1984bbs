																											<div id="postmessage_812746" class="t_msgfont">伊斯兰恐怖组织在全世界的恐怖活动可谓臭名昭著。伊斯兰恐怖分子引爆和摧毁 建筑物、飞机和交通工具。而且，穆斯林恐怖分子在过去的二十年当中，已经攻击 和杀害了成千上万的男女，包括大人和小孩。穆斯林恐怖分子在全世界各个角落，包 括肯尼亚、阿尔及利亚、印度尼西亚、埃及、伊朗、法国、南美洲和美国等国家，袭 击和谋杀他们感觉对他们的目标构成威胁的人。没有人能够幸免不受这些奸诈的人伤 害。 <br />
<br />
不要惊讶，只要查看一下有关恐怖主义的网站，就可以显示世界上所有的恐怖组 织，大约有一半在本质上是伊斯兰组织。 <br />
<br />
为甚么这些组织是伊斯兰的呢？伊斯兰这个宗教为甚么总是与恐怖主义相关呢？这 两者之间是否有甚么关系呢？这些组织怎样以伊斯兰的观念来证明谋杀平民是正当的呢 ？伊斯兰是否确实允许恐怖主义和谋杀呢？ <br />
<br />
本文是要探究在伊斯兰当中恐怖主义的根源。我会从穆罕默德、他的教义和他的行 为开始讲起，然后看看其他的穆斯林是怎样教导的，就能够查验出伊斯兰恐怖主义的根 基。 <br />
<br />
我们看到穆罕默德杀害了很多人。许多人因着穆罕默德的要求、命令和指示被杀， 不少人是死于睡梦中。没有审讯、没有裁判、没有对话。你要是污辱了穆罕默德，你要 是怀疑他是否可信，你要是大胆地说出来，你就会被杀害。许多男女老少被杀害，因为 穆罕默德憎恨那些公然反对他的人。 <br />
<br />
以下概括了在穆罕默德的命令或努力下犯下的这七宗恐怖谋杀∶ <br />
<br />
1） 伊本 Sunayna ，一个犹太人被杀，纯粹因为他是个犹太人。 <br />
2） 艾布 Afak ，一个一百二十岁的老人，睡觉时被杀。 <br />
3） 艾斯玛麦尔旺，五个孩子的母亲，睡觉时被杀。 <br />
4） 一个女奴隶，两个孩子的母亲，睡觉时被杀。 <br />
5） 一个独眼的牧羊人，睡觉时被杀。 <br />
6） 一个老妇女，穆斯林侵略时抓的俘虏，被撕成两半。 <br />
7） 一个女奴隶，因为嘲讽穆罕默德而被杀。 <br />
<br />
穆罕默德早期的行为和教义 <br />
<br />
当穆罕默德开始宣讲他的穆斯林信仰时，还不具暴力性。他因着宣扬他的宗教思想 C 伊斯兰 C 和诋毁麦加人的多神教而受到迫害。一些追随穆罕默德的人受到折磨。形 势对他和他少数的追随者相当不利，因此他派遣他们当中许多人到阿比西尼亚（ Abyssinia ） 〔埃塞俄比亚〕 避难。最后，他和他的追随者向北迁移到了一个叫叶斯 里卜 （ Yathrib ） 〔麦地那〕 的城市，在那里有两个阿拉伯部落的一些人想让穆罕 默德作他们的先知。 <br />
<br />
穆罕默德暴力的开始 <br />
<br />
就在穆罕默德起身前往麦地那之前，他得到了一个允许他与麦加人战斗的 “启示” 。他知道到了麦地那，就会有一帮武装的人支持他。而且，麦地那离麦加人远，他们想 压迫和杀害他的企图更难以得逞。下面的内容是摘自 A. Guillaume 所著的 《穆罕默 德生平》 ，第212页，它是译自一个早期穆斯林学者伊本伊斯哈格所著的穆罕默德传记 《 Sirat Rasulallah 》 。 〔1〕 <br />
<br />
“使者收到战斗的命令” <br />
<br />
在第二次亚喀巴 （ Aqaba ，地名，穆罕默德与来自麦地那跟随他的人立约之地） 之前，使者并没有得到允许去作战或者流人的血。他只是受命呼召人们归向真主，并且 忍受凌辱和赦免无知的人。古莱什人（麦加人当中的主要团体） 迫害跟随他的人，诱使 某些人离弃他们的信仰，并驱逐其他人离开他们的家园。他们必须选择是放弃他们的信 仰，还是留在国内受虐待，或者是逃亡他国，有些人去了阿比西尼亚，其他人去了麦地 那。 <br />
<br />
当古莱什人对真主变得傲慢无礼，并拒绝k赐恩的目的，指控k的先知撒谎，并恶 待和驱逐那些服事k、宣称k为独一的真主、相信k的先知和坚信k的人，到了这个时 候，k才允许k的使者去与那些侮辱和恶待他们的人战斗，以保护自己 … 〔 a 〕 <br />
<br />
意思就是 “我允许他们去战斗，只因为他们受到不公正的对待，而他们唯一冒犯人 们的地方就是敬拜真主。当他们占优势地位的时候，他们就将谨守拜功、完纳天课、劝 善戒恶，也就是说，先知和他们所有的同工。”然后真主赐他这句话语∶ “你们当与他 们战斗，直到诱惑消除，” [ b ] 也就是说，直到没有一个信士会被诱使脱离他的信 仰。 “而宗教专为真主”，亦即 “直到唯有真主受到敬拜” 时。 <br />
<br />
这里有两个关键点∶ <br />
<br />
1） 在麦加的时候，穆罕默德是软弱的，他没有攻击任何人。他只是传扬他的宗教并 侮辱麦加人的宗教。但就在他迁往麦地那的前夕，他有了一帮数量有限的武装人士支持 他，他就得着了这个 “启示” ，开始使用暴力来促进他的欲望。伊斯兰的历史向我们 表明，随着伊斯兰势力的壮大，他们的暴力形式也在发生改变，从犯罪形式的恐怖行为 发展到直接的战争行为。 <br />
<br />
2） 在引文的结尾处，说到穆斯林可以攻打那些不敬拜安拉的人。 <br />
<br />
我还想评论一下伊本伊斯哈格的著作。当我读到伊本伊斯哈格所写的这段话时，穆罕 默德被塑造成一个长期受苦的人，他反抗的根本目的是为了自我防御，一直到穆罕默德 离开之前，他都忍受得住麦加人的迫害，但最后糟糕到穆罕默德得到反抗的许可。 <br />
<br />
问题是，在穆罕默德离开一段时间之前，他和穆斯林已经受到严重的迫害和虐待。换 句话说，上面引用的话是伊本伊斯哈格为辩护而写的。在较早期，亚喀巴条约（ Treaty of Aqaba ） 签订一段时间之前，处境对穆罕默德来说是如此的艰难，以至他要跑到一个 叫塔伊夫的镇上寻求他们的帮助和保护〔 Guillaume ，出处同上，第192页〕 。但塔伊 夫人也拒绝他和虐待他。在麦加，穆罕默德的处境是多么的艰难，以致他不得乞求三个人 保护他〔 Guillaume ，出处同上，第194页〕 。 <br />
<br />
