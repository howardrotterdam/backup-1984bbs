讨论 <br />
<br />
让我们来总结一下，并且正确地观察此事。 <br />
<br />
穆罕默德把哈里斯本苏韦德本 Samit 杀了。这激起了艾布 Afak 的不满，所以他公 开反对此事。所以，穆罕默德也同样地将艾布 Afak 谋杀了。这又触怒了艾斯玛本麦尔 旺，她也大胆地反对这种罪恶行径。她鼓励她部落里的同胞采取行动反抗穆罕默德。当 穆罕默德听到她所说的话，就把她也杀了。 <br />
<br />
现在，乍看之下，杀害艾斯玛的命令似乎有点无可非议。艾斯玛呼召一些人去杀死 穆罕默德。但从穆罕默德的观点来看，她的呼召给他带来麻烦，这点似乎是可以理解的 。 <br />
<br />
因此，让我们更深入探讨这件事，并查考一下有关艾斯玛与她部落关系的背景。 <br />
<br />
1） 首先，艾斯玛看透了穆罕默德的行动。她看透了他的为人，是一个冷血无情的 杀人凶手。她当然要公开反对一个杀人犯。 <br />
<br />
其次，她的部落不在穆罕默德的统治之下。也许他们与穆罕默德有个条约，也许没 有。无论哪种情况，这个女人都有发表意见的自由。如果有一个条约存在，而她越轨的 话，穆罕默德可以向她部落的领袖投诉她，他们就会命令她闭口或者控制事态。 <br />
<br />
2） 这事件更值得一提的，就是在她被杀之后，穆罕默德说∶ “不会有两头山羊用 们的角来撞她的头。” 意思就是没有人会在乎她的死。 （当然是除了她的孩子和家 人以外）。 <br />
<br />
还要注意的是，她的部落中已经有人成为穆斯林。这些人就当然不会听她的话了。 <br />
<br />
