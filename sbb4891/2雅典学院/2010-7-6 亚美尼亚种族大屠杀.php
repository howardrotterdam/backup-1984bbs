													<h2>亚美尼亚种族大屠杀</h2>
						
						
																											<div id="postmessage_724407" class="t_msgfont">亚美尼亚种族大屠杀（亚美尼亚语：&#1344;&#1377;&#1397;&#1400;&#1409; &#1361;&#1381;&#1394;&#1377;&#1405;&#1402;&#1377;&#1398;&#1400;&#1410;&#1385;&#1387;&#1410;&#1398;；土耳其语：Ermeni Soyk&#305;r&#305;m&#305;），指土耳其政府于1915年至1917年间，对其辖境内亚美尼亚人进行的屠杀和强行迁移行为。其受害者数量达到150万之众。<br />
<br />
土耳其政府至今拒绝承认这是一起官方发起的有预谋的屠杀行为，但亚美尼亚、俄罗斯和其他多数西方国家皆认为这是一起可以和犹太人大屠杀相提并论的由一国政府蓄意进行的种族灭绝行为，一些土耳其学者（如奥尔汉・帕穆克等）也持有相同观点。目前共有阿根廷、法国、黎巴嫩、荷兰、瑞典等22个国家官方承认这是一场种族屠杀。联合国、欧洲议会、比利时、法国、希腊和俄罗斯均承认“亚美尼亚大屠杀”事件，称这宗屠杀为“违反人性的罪行”。联合国于1978年将事件定性为“种族灭绝”[来源请求]。<br />
<br />
事前态势<br />
奥斯曼帝国的亚美尼亚人<br />
 <br />
大屠杀前安那托利亚中东部及南高加索的亚美尼亚人聚居地区 <br />
亚美尼亚人口主要集中在帝国东部在奥斯曼帝国内，根据伊斯兰教的齐米（dhimmi）制度，作为基督教徒的亚美尼亚人可享受有限度的自由，例如信奉原有宗教的权利，但是待遇有如二等公民。基督教徒及犹太人不被视为与穆斯林平等，前两者指控穆斯林的供词不会被法庭接纳。基督教徒及犹太人被禁止携带武器或骑马，他们的房屋也不能高过穆斯林的房屋，宗教活动也要顺从穆斯林的规定，此外还有其它法律上的限制。[1]违反这些法令的人可被处罚，轻则罚款，重者可被处死。<br />
<br />
欧洲三大强国英国、法国及俄国（下称“列强”）对奥斯曼帝国处理国内的基督教徒少数族裔有异议，向奥斯曼政府施压日增，要求它的所有公民都享有同等权利。从1839年起，奥斯曼政府推行坦齐马特改革去改善少数族裔的处境，但成效不大。到了1870年代末，希腊及几个巴尔干国家已经不能忍受，先后摆脱了奥斯曼的统治。亚美尼亚人在这段时间没有积极争取独立，使他们得到了“忠心米利特”（土耳其语：millet-i sad&#305;ka）的称呼。[2]<br />
<br />
推行改革，1860年代至1880年代<br />
<br />
从1860年代中期至1870年代初，亚美尼亚人开始要求奥斯曼政府改善他们的待遇。亚美尼亚人群体议会（Armenian Communal Council）从安那托利亚东部的农民收集签名后，向奥斯曼政府请愿，要求纠正被那些农民抱怨的事：库尔德人及索卡西亚人在亚美尼亚人市镇抢掠和杀人；不依法收税；政府官员的犯罪行为；拒绝在审讯时接纳基督教徒作为证人。[3]奥斯曼政府听取了这些诉求，承诺会惩罚那些要对事件负责的人。[3]<br />
<br />
1875年，波斯尼亚和黑塞哥维那、保加利亚及塞尔维亚的基督教徒起事被镇压后，列强援引1856年的《巴黎条约》，宣称该条约容许它们介入以保护奥斯曼帝国的基督教徒少数族裔。[4]面对日增的压力，政府宣布实行君主立宪制及与列强谈判。与此同时，亚美尼亚人的君士坦丁堡牧首拿施斯二世（Nerses II）向列强提出亚美尼亚人的控诉，指他们遭受广泛的“强夺土地……强迫妇女及儿童改变宗教信仰、纵火、强索保护费、强奸及谋杀”。[5]<br />
<br />
在1877年至1878年的俄土战争结束后，亚美尼亚人开始更倾向以俄国作为他们安全的保证人。拿施斯于俄国领导人与奥斯曼人在圣斯特凡诺谈判期间，接触了俄国人，说服他们在《圣斯特凡诺条约》加入第16条，规定占据亚美尼亚省份的俄军只会在奥斯曼完全落实改革后才会撤走。[6]英国不想俄国占据大量奥斯曼领土，迫使俄国进行新谈判，于1878年6月13日召开柏林会议。亚美尼亚人参与了谈判，表明他们争取的是自治，不是脱离奥斯曼帝国独立。[7]他们取得部份成功，《柏林条约》第61条包括了上次第16条的同样文句，但是删去俄军可留在那些省份的提及，取而代之是奥斯曼政府须定期把改革的进度知会列强。<br />
<br />
哈米德大屠杀，1894年至1896年<br />
 <br />
1895年在Erzerum被屠杀的亚美尼亚人1876年，新苏丹哈米德二世登位。从《柏林条约》签订后的改革期开始，哈米德二世试图搁置那些改革，声称亚美尼亚人在那些省份未占大多数，又称亚美尼亚人报告受苛待的事大多是夸大或不实。1890年，哈米德二世创建了一支准军事队伍“哈米迪耶”（Hamidiye），由库尔德族非正规人员组成，任务是“随他们想怎样的去处理亚美尼亚人”。[8]由于奥斯曼官员蓄意在亚美尼亚人聚居市镇引发叛乱（通常是因为税收过重），例如1894年的“沙逊抵抗”（Sasun Resistance），这些部队越来越多被用作以压制及屠杀手段去对付亚美尼亚人。亚美尼亚人成功击退这些部队，于1895年提请列强关注这些越轨行为，而列强随即谴责奥斯曼政府。[9]<br />
<br />
列强迫使哈米德二世于1895年10月签署新的一篮子改革，以抑制“哈米迪耶”的权力，可是与《柏林条约》规定的改革一样，从未得到落实。1895年10月1日，2000个亚美尼亚人在君士坦丁堡集会请愿，要求落实改革，遭奥斯曼警察用武力驱散。[10]未几，君士坦丁堡发生了一些亚美尼亚人被屠杀事件，随后席卷其他亚美尼亚人聚居的比特利斯省、迪亚巴克尔省、Harput省、锡瓦斯省、特拉布宗省及凡城省。遇害的亚美尼亚人数目有不同估计，但是欧洲人对这次被称为哈米德大屠杀的暴行的文件记录把数字定于10万至30万人之间。[11]<br />
<br />
虽然哈米德二世未有直接下令进行那些屠杀，但是他被怀疑默许它们发生及没有采取行动制止。[12]对于欧洲人漠视那些屠杀而感到气馁的亚美尼亚革命联盟成员于1896年8月26日占据欧洲人管理的奥斯曼银行。该事件为亚美尼亚人在欧洲带来更多的同情，被欧美新闻界赞扬，他们把哈米德二世描绘成“大杀手”及“血腥苏丹”。[13]虽然列强誓言会采取行动及强制实施新的改革，却因为政治及经济利益的缘故而没有实现。<br />
<br />
 青年土耳其人革命，1908年<br />
1908年7月24日，哈米德二世被军官发动政变推翻，恢复君主立宪制，再次使亚美尼亚人重燃在帝国内享有平等权利的希望。政变者来自青年土耳其人运动，欲改革帝国的施政及推行现代化以追上欧洲水平。该运动由两大类人组成：世俗主义的自由立宪主义者，及民族主义者。前者比较支持民主，愿意接纳亚美尼亚人加入他们；后者则对亚美尼亚相关议题及亚美尼亚人常找欧洲人求助的事采取较不容忍的立场。[14]在1902年于巴黎举行的一次青年土耳其人大会上，自由派的首领Sabahheddin Bey及Ahmed Riza部份地说服民族主义者把“确保国内所有少数族裔享有某些权利”列入他们的目标之中。<br />
<br />
在青年土耳其人的众多派系中，有一个叫“统一与进步委员会”（Committee of Union and Progress，简称CUP）的政治组织。该组织最初是一个由萨洛尼卡的军官组成的秘密结社，在军方内日渐壮大。<br />
<br />
<br />
阿达纳屠杀<br />
 <br />
在1909年阿达纳屠杀时遭受抢掠及破坏的一个亚美尼亚人城镇1909年4月13日发生了一场反政变，一些奥斯曼军官与伊斯兰神学生联手，意图把国家回复苏丹及伊斯兰法的统治。反动派部队与新政府部队发生战斗，最后新政府平息了叛乱，把反对派领导人送交军法审讯。<br />
<br />
该事件虽然最初是针对新成立的青年土耳其人政府，却殃及亚美尼亚人，他们被视为支持恢复宪法而遭受杀戮。[15]当奥斯曼军队被召来时，许多记载指他们没有试图制止暴行，反而加入抢掠阿达纳省的亚美尼亚人飞地。[16]约15000至30000个亚美尼亚人在“阿达纳屠杀”中遇害。[17][18]<br />
<br />
 亚美尼亚人种族灭绝，1915年至1917年<br />
 <br />
亚美尼亚人的乱葬岗，1915年1914年11月，奥斯曼帝国加入第一次世界大战的同盟国一方。战争部长恩维尔・帕夏（Enver Pasha）的部队在萨瑞卡密斯战役被俄军打得大败，他返回君士坦丁堡后，公开指责萨瑞卡密斯那里的亚美尼亚人主动协助俄国人，导致他打败仗。[19]<br />
<br />
 劳动营，2月25日<br />
1915年2月25日，战争部长恩维尔下令所有军方单位把部队内的亚美尼亚人移走，改编他们入无武装的劳动营。恩维尔解释此举为“恐怕他们会串通俄国人”。传统上奥斯曼陆军只会征召20至45岁的男性非穆斯林入正规军，较年幼（15至20岁）及较年老（45至60岁）的非穆斯林士兵会编入劳动营作为后勤支援。在2月以前，有些入伍的亚美尼亚人当上了劳工，最终也难逃一死。[20]<br />
<br />
把部队的亚美尼亚兵士改送到被动的、无武装的后勤部门是随后的种族灭绝的重要一步。根据《The Memoirs of Naim Bey》所述，消灭那些营的亚美尼亚人是以“统一与进步委员会”名义进行的有预谋计划的一部分，那些亚美尼亚兵中有许多人被当地的土耳其人团伙杀害。[21]<br />
<br />
凡城事件，1915年4月<br />
 <br />
1915年5月，武装亚美尼亚平民及自卫单位在凡城据守战线，抵抗土耳其部队1915年4月19日，凡城省长杰夫戴特・贝伊（Jevdet Bey）以征兵为理由，要求凡城立即向他提供4000人。亚美尼亚人认为他有阴谋，没有依从。杰夫戴特早前已在附近村庄展开屠杀，表面上用搜查武器为借口。[22]亚美尼亚人提议交出500人再加豁免金代替其余的人，以换取时间，可是杰夫戴特指控亚美尼亚人“叛乱”，表示他有决心不惜任何代价去“粉碎”它。他宣称：“如果叛乱分子敢开一枪，我就杀掉每个基督教徒男人、女人及（指向自己膝盖）有这么高的每个儿童。”[23]<br />
<br />
1915年4月20日，当一个亚美尼亚女人遭调戏，两个前来帮她的亚美尼亚男人被土军杀死后，凡城的武装冲突开始。亚美尼亚防守者有1500个枪手，配备300支步枪、1000支手枪及其它陈旧武器，保护了亚美尼亚居民3万人及难民1万5千人。后来尼古拉・尤登尼奇的俄军前来解救他们，冲突才结束。[24]<br />
<br />
拘捕亚美尼亚显要人物，1915年4月<br />
1914年的奥斯曼当局已经开始在政治宣传上把帝国内的亚美尼亚人形容为对帝国安全的威胁。战争部一个海军军官这样形容该计划：<br />
<br />
In order to justify this enormous crime the requisite propaganda material was thoroughly prepared in Constantinople. [It included such statements as] &quot;the Armenians are in league with the enemy. They will launch an uprising in Istanbul, kill off the Committee of Union and Progress leaders and will succeed in opening the straits (of the Dardanelles).&quot;[25]<br />
<br />
1915年4月24日晚，奥斯曼政府一夜间拘捕了二百多位君士坦丁堡的亚美尼亚知识分子及社区领袖，[26]许多人在后来被杀害。<br />
<br />
特西尔法<br />
1915年5月，内政部长塔拉特・帕夏（穆罕默德・塔拉特）要求内阁及大维奇尔Said Halim Pasha立法准许迁移及重新安置亚美尼亚人到其它地方，理由是“国内有些地方出现了亚美尼亚人的暴乱及屠杀”。塔拉特特别提到凡城事件，将措施扩展至“暴乱及屠杀”有可能影响高加索战区安全的地区。后来迁徙居民的范围扩大至其它省份的亚美尼亚人。1915年5月27日，奥斯曼议会通过《特西尔法》（Tehcir Law，或称《驱逐临时法》），容许政府及军方驱逐任何被认为威胁国家安全的人，但是设有条款保障被驱逐者的财产权。到9月又有新法例《关于被驱逐者遗留的财产、债务及资产法》（又被称为《征用及充公临时法》）被提出，根据该法例，亚美尼亚人所有“遗留”的货物及财产归政府所有。奥斯曼议会代表Ahmed Riza反对该法例，他说：<br />
<br />
It is unlawful to designate the Armenian assets as &quot;abandoned goods&quot; for the Armenians, the proprietors, did not abandon their properties voluntarily; they were forcibly, compulsorily removed from their domiciles and exiled. Now the government through its efforts is selling their goods... If we are a constitutional regime functioning in accordance with constitutional law we can't do this. This is atrocious. Grab my arm, eject me from my village, then sell my goods and properties, such a thing can never be permissible. Neither the conscience of the Ottomans nor the law can allow it.[27]<br />
<br />
1915年9月13日，奥斯曼议会通过新法例，亚美尼亚人的所有财产可被政府充公。[28]<br />
<br />
驱逐及灭绝过程<br />
 <br />
各个屠杀地点、驱逐及灭绝中心《特西尔法》实施后，亚美尼亚人遭杀戮及充公财产的事在许多西方国家激起不满。虽然奥斯曼帝国的战争盟邦没有多大抗议，许多德国及奥地利历史文件仍记载了目击者对亚美尼亚人被杀害及普遍挨饿的可怕经历。[29][30][31]美国《纽约时报》在1915年间有过百篇关于亚美尼亚人被集体谋杀的文章，形容屠杀是“有系统”、“获得许可”及“由政府组织的”。老罗斯福后来说那是“战争中最大的罪行”。[32]<br />
<br />
亚美尼亚人的队伍被迫前往叙利亚的Deir ez-Zor镇及周围荒漠。不少证据显示奥斯曼政府在驱逐过程中没有向亚美尼亚人提供任何补给设施或物资，即使抵达目的地后也是如此。[33]到1915年8月，《纽约时报》复述一份出处不明的报告，指“流徙者的尸体遍布道路及幼发拉底河，那些侥幸生还的人也是注定死路一条，这是一个灭绝全部亚美尼亚人的计划。”[34]<br />
<br />
负责押送亚美尼亚人的奥斯曼军队不仅纵容其他人抢劫、杀害及强奸那些亚美尼亚人，许多时甚至自己也有份参与其中。[33]既被夺去随身财物，又被赶入荒漠的亚美尼亚人，死者数以十万计。<br />
<br />
Naturally, the death rate from starvation and sickness is very high and is increased by the brutal treatment of the authorities, whose bearing toward the exiles as they are being driven back and forth over the desert is not unlike that of slave drivers. With few exceptions no shelter of any kind is provided and the people coming from a cold climate are left under the scorching desert sun without food and water. Temporary relief can only be obtained by the few able to pay officials.[33]<br />
<br />
 <br />
被赶向东方的亚美尼亚人，只能携带这些东西相信有25个主要的集中营存在过，听令于塔拉特的一位左右手&#350;ükrü Kaya。[35]那些营大多数接近现代土耳其与伊拉克及叙利亚接壤边界，有些只是临时中转营。[35]其它的，诸如Radjo、Katma及Azaz，据称只是临时作为乱葬岗之用，这些地点于1915年秋天被空置。[35]有些作者也认为在Lale、Tefridje、Dipsi、Del-El及Ra's al-'Ain的营是特地为那些只能再活多数天的人而建。[35]<br />
<br />
虽然几乎所有营，包括那些主要营，都是露天的，在较小的营发生的其余集体杀害事件不仅有直接杀害，也有集体烧死。Eitan Belkind是一位渗透入奥斯曼军队担任军官的Nili成员，被派往Camal Pasha的总部，宣称曾目睹5000个亚美尼亚人被烧死。[36]奥斯曼军队的Hasan Maruf中尉描述有一条村的居民被集中到一起，然后被烧死。[37]奥斯曼第三军团司令韦希布的12页书面供词（日期为1918年12月5日）在特拉布宗连串审讯时（1919年3月29日）呈堂，被包括在主要起诉书内，[38]报告穆什附近发生过一宗这样子的全村人被集体烧死。<br />
<br />
 <br />
在阿勒颇附近，一个亚美尼亚妇女跪在死去孩子的旁边[编辑] 特别组织<br />
 <br />
在埃尔津詹被屠杀的亚美尼亚人的骸骨[39]由“统一与进步委员会”建立的一个名叫“特别组织”（土耳其语：Te&#351;kilat-i Mahsusa）的特别部队，有份参与摧毁奥斯曼的亚美尼亚人社群。[40]从1914年底到1915年初，奥斯曼政府从监狱释放数以千计的囚犯，让他们加入该组织，这些人后来负责押送被赶离家园的亚美尼亚人队伍。奥斯曼第三军团司令韦希布・帕夏曾称“特别组织”的成员是“人类的屠夫”。[41]<br />
<br />
 当时的报告及反应<br />
后事<br />
土耳其的军事法庭<br />
1919年，苏丹穆罕默德六世下令国内的军事法庭就帝国卷入第一次世界大战的责任对“统一与进步委员会”的成员展开审讯。军事法庭指责“统一与进步委员会”的成员进行一场不符合米利特概念的战争。亚美尼亚人的事件被掀出来成为指控。在这些审讯中产生的文件大多在后来被移交到国际性审讯那里。1919年1月，一份呈上穆罕默德六世的报告指控了超过130个疑犯，大多数是高官。军事法庭认定“统一与进步委员会”蓄意通过“特别组织”在肉体上消灭亚美尼亚人。<br />
<br />
“三大帕夏”，包括塔拉特及恩维尔，在一次大战结束时逃到国外。1919年于君士坦丁堡举行的审讯把缺席的这三人判处死刑。军事法庭解散了“统一与进步委员会”，充公了它及那些被判有罪的人的资产。“三大帕夏”中有两人后来被亚美尼亚复仇者刺杀。<br />
<br />
 国际审讯<br />
《穆德洛斯停战协定》签订后，在巴黎的初步和平会议于1919年1月成立了“责任及制裁委员会”，由美国国务卿罗伯特・蓝辛担任主席。根据该委员会的工作成果，数项条款被加进《色佛尔条约》内，奥斯曼帝国的代理政府、苏丹穆罕默德六世及大维奇尔Damat Adil Ferit Pasha被传召出席审讯。1920年8月的《色佛尔条约》有进行审讯的计划，以决定谁人要为“野蛮及不合法的战争方法……（包括）违反战争法与惯例及人道原则的事项”负责。[42]该条约第230条规定奥斯曼帝国须向盟国移交那些被盟国认为要对战时在帝国境内发生的屠杀事件负责的人。<br />
<br />
多位奥斯曼的政客、将领及知识分子被移送到马耳他，他们在那里被扣留约3年。与此同时，调查他们所为的翻查档案工作在君士坦丁堡、伦敦、巴黎及华盛顿进行。[43]可是《色佛尔条约》规定的盟国审讯最终没有实现，被扣留的人被送返土耳其，以换取土耳其凯末尔新政府释放被扣留的英国公民。<br />
<br />
 所罗门・特利里扬的审讯<br />
 <br />
被刺杀的塔拉特・帕夏1921年3月15日，前大维奇尔塔拉特・帕夏在光天化日下于德国柏林被亚美尼亚人所罗门・特利里扬刺杀，后者在德国法庭以谋杀罪受审。特利里扬承认杀人，律师以被告的精神状况受亚美尼亚种族大屠杀影响作为辩护，最后陪审团以他有“暂时性精神错乱”而判他罪名不成立。<br />
<br />
亚美尼亚人的死亡数字，1914年至1918年<br />
虽然对于在大屠杀中丧生的亚美尼亚人数目仍未有共识，西方学者普遍认为有超过50万亚美尼亚人在1914年至1918年间死亡。估计从30万人（现代土耳其国的说法）至150万人（现代亚美尼亚国[44]、阿根廷[45]及其它国家）不等。《大英百科全书》参考了阿诺尔德・约瑟・汤因比研究得出的从1915年至1916年“估计约60万亚美尼亚人在驱逐过程中死亡或被屠杀”。[46][47]<br />
<br />
对亚美尼亚种族大屠杀的研究<br />
希伯来大学学者耶胡达・鲍尔（Yehuda Bauer）认为亚美尼亚种族大屠杀“最能够与犹太人大屠杀相提并论”。[48]不过他也指出两者有几点明显分别，尤其在动机方面。<br />
<br />
鲍尔也指出亚美尼亚种族大屠杀应被理解为是1896年开始，经历1908/09年、第一次世界大战及直至1923年的种族灭绝，而非于1915年才开始。[49]<br />
<br />
Bat Ye'or认为“亚美尼亚人的种族灭绝是一场圣战”。[50]她指圣战及所谓的“齐米心态”（dhimmitude）是导致亚美尼亚种族大屠杀的“原则和价值观”的一部份。[51]不过当时目睹亚美尼亚人受迫害的Fà'iz el-Ghusein有不同看法，认为亚美尼亚人的苦难是统一与进步委员会所为，起自民族主义狂热及妒忌亚美尼亚人，与伊斯兰教信仰无关。阿诺尔德・汤因比及其它一些资料来源报告说许多亚美尼亚人通过与土耳其人结婚或改信伊斯兰教而幸免一死。El-Ghusein指出许多改信者仍被杀害，担心西方人会认为“亚美尼亚人的灭绝”是“伊斯兰教历史上一个抹不掉的污点”。[52]曾经有伊斯兰教领袖呼吁放过那些改信伊斯兰教的亚美尼亚人，El-Ghusein引述一位政府官员在杀死改信者前回应说“政治的事没有宗教”。[53]<br />
<br />
土耳其共和国的立场<br />
土耳其共和国的正式立场是亚美尼亚人在“迁移”或“驱逐”过程中的死亡不应被视为“种族灭绝”，理由分别有：那些杀人事件不是由政府蓄意策划的；亚美尼亚人作为一个文化上的群体，可能会同情俄国而构成威胁，杀死他们是合理的；亚美尼亚人只是挨饿；或指有“亚美尼亚匪帮”作恶之类等。[54][55][56]有些人用语义或“不适用于那个时代”作为理由去否定这次屠杀是“种族灭绝”（英文的genocide一字直到1943年才诞生）。<br />
<br />
土耳其在一次大战的伤亡数字常被用来淡化亚美尼亚人的死亡数字。[57]<br />
<br />
土耳其政府的资料又称有历史可证明的“土耳其人的宽容”[58]使得亚美尼亚种族大屠杀根本不可能发生。一份军方文件用11世纪的历史去反驳这次种族大屠杀：“是塞尔柱土耳其人于1071年对亚美尼亚人的统治使他们脱离拜占庭帝国的迫害，并容许他们过正常生活。”[58]<br />
<br />
2005年，土耳其总理雷杰普・塔伊普・埃尔多安邀请土耳其、亚美尼亚及国际上的历史学者成立一个委员会，利用两国及其它国家的档案去重新评估“1915年的事件”。[59][60]亚美尼亚总统罗伯特・科恰良拒绝了埃尔多安的提议。<br />
<br />
此外，时任土耳其外长的阿卜杜拉・居尔邀请美国及其它国家委派学者为该个委员会作出贡献，“调查这次惨剧及打开通道让土耳其人与亚美尼亚人走到一起”。[61]<br />
<br />
土耳其政府仍在抗议其它国家正式承认这次屠杀是种族灭绝，及质疑根本不存在种族灭绝。<br />
