													<h2>评《鬼子来了》</h2>
						
						
																											<div id="postmessage_812342" class="t_msgfont">在9月3号这个特殊的日子，看了这部电影。心情实在难以平静。所以在深夜还是匆匆写下这些文字。<br />
不得不说，这是一部非常优秀的作品，其实早已厌倦了这个题材的影视作品，记得袁腾飞曾说过，在中国的影视作品里，抗战是一件无比轻松的事，地道战，地雷战等等，日本鬼子被打的屁滚尿流，一个八路军都能消灭成百上千的鬼子。小时候觉得挺好玩，现在实在是觉得太过恶心，看那些东西实在是对自己智商的侮辱。而这部片子实在是深深的震撼了我，能够做到如此尊重历史，符合逻辑，真实再现当时情景的作品实在太少。我是一个唐山人，在寝室不停的说，姜文实在是我们唐山的人才啊！它逗笑了我，也深深的刺痛了我，更有深深的共鸣。<br />
真的太亲切了，唐山话的对白，比《唐山大地震》里的唐山话正宗很多；影片里那些百姓的姿态，让我不禁想起自己曾经目睹，经历的人与事，过去了这么多年，没有什么改变，鲁迅说的对，中国自始至终，一直经历着两个时代。一，想做奴隶而不得的时代； 二，暂时做稳了奴隶的时代。他们当过满族人的奴隶，当过军阀的奴隶，当过日本人的奴隶，现在依然乖乖的做着gcd的奴隶。自由算什么东西，尊严又值几个钱。还是保住命要紧，所以他们乖乖的伺候着鬼子，伺候着汉奸。这种“人道”根本不是出于对战俘的仁慈，如果真的仁慈恐怕最后也不会去宰那些手无寸铁的战俘了吧。那种仁慈不是出于“仁爱”，而是自私之爱，因为他们怕那个找上门的“我”，怕不能完成“我”交代的任务。如果说，日本鬼子找上门，拿着枪，他们一样忠心耿耿的为鬼子卖命。说实话，我痛恨这些没有骨气的“支/那/猪”。而这些高傲的”支/那/猪&quot;却总是拿出无辜，拿出善良来掩饰自己的懦弱，掩盖自己的势利。以为，皇军会宽待忠诚的奴隶，以为上天会眷顾善良的生物。但是在不平等的前提下，奴隶的命运永远不会掌握在自己的手上。<br />
我恨这些&quot;支/那/猪&quot;，有时这种恨超过了对日本鬼子的仇恨。静下心想想，这些老百姓的确挺可怜的，招谁惹谁了呢？老百姓不奢求高官厚禄，没有为了金钱权势出卖自己的良心，去做汉奸走狗，出卖亲人去给皇军通风报信。这些老百姓确实挺可怜的。国军力量本身就不强，正面战场做出了巨大的牺牲，但依然无法挽回失败的战局。还有很多叛徒当了伪军，替日本鬼子卖命，带头欺压百姓。传说中的共军也一向是飘忽不定，打一枪换一个地方。老百姓没有依靠，也自然成了墙头草，只想安安稳稳过个好日子。<br />
现在，想想我们就注定要一直悲剧下去吗？这部电影能带给我们什么呢？牢记历史，勿忘国耻。这个谁都知道。鬼子可能是日本人，可能是俄国人，可能是美国人，同样可能是你的那些骨肉同胞。什么才是老百姓可以依靠的？日本人？俄国人？美国人？还是我们自己的政/府？可能大家比较倾向自己的政/府，按照情理也应如此。但历史警告我们，这个政/府必须是我们老百姓自己的政/府，也就是必须是实际中代表并维护人民利益的政/府，如果百姓只是被代表，那只能“（国）兴，百姓苦；（国）亡，百姓苦”，因为天朝的好坏跟p民没有什么关系，那是被个别利益集团把持的“家天下”。日本在变，俄国在变，美国在变，我们也会变。过去日本，俄国，美国都当过鬼子，我们也当过吧。所以没必要把鬼子完完全全的定义为某个国家。鬼子是个幽灵，常常会寄生于某个国家。当然，这是不好的。反鬼子，不是该反某个国家，而是反这个灭绝人性的“鬼魂”。对于我们这个懦弱没有精神的民族而言，成为鬼子的机率倒不是太大，被鬼子欺负的可能性更大些。如果我们无法剔除“支/那/猪”的贪生怕死，苟且偷生的恶习，那么老百姓将永远的悲情下去。如果这是一部戏剧，或许煽情而动人，如果不断的真实发生呢？<br />
呵呵～</div>

							
							
							
							
