2、本人没有使用私家车非法营运的动机，以前也从没有使用私家车非法营运的行为。6月13日发生的事情主要是由于南汇城市执法大队所雇佣的钩子诱惑所致，我之所以上当只能说明我身上也有大部分人具有的人性弱点──贪小便宜。此处需要重申的是：是一个男的主动拉我车门，请求我帮忙送他一下，并不是我动揽客，也是他主动要给我钱，而不是我跟他要钱。而且最后到达其目的地时他没给我钱，我也没有跟他要钱。没有事实交易。 <br />
<br />
3、&nbsp;&nbsp;南汇城市交通执法大队认定我13日非法营运的证据是非法获得，应不具效力。我国刑法第43条规定：“严禁刑讯逼供和以威胁引诱欺骗以及其他非法的手段收集证据。”南汇城市交通执法大队采用无耻的放倒钩的方法来收集证据的行为与法定的正当的程序不符。其所依据扣押我车的所谓证据不能作为合法证据。 <br />
<br />
