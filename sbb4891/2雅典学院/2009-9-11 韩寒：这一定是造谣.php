&nbsp; &nbsp;&nbsp; &nbsp;您们好<br />
&nbsp; &nbsp;&nbsp; &nbsp;今天我怀着无限愤懑的心情给您写下这封邮件，我不知道您百忙之中是否能看到。但作为一个遵纪守法的公民，上海市民，我今天所遭受的对待是我无法相信这是我们D的行政机关所为，也是我一生中遭遇的最不公平的对待。<br />
