																											<div id="postmessage_780783" class="t_msgfont">转帖：<br />
<br />
美国是一个很反共的国家。根据美国早期的移民法规定，凡是隶属于共产党及其所属组织者，均不得移民美国。在入境之后，如果被发现隶属共产党，将被递解出境。感谢冷战的结束，美国在90年代修订了移民法案中一些有关共产党员入境及移民的规定。新规定放松了禁止共产党员入境的有关规定。 但在I-485的申请表中还是要你申报你是否增经加入过共产党。<br />
<br />
见：<a href="http://www.uscis.gov/files/form/i-485.pdf" target="_blank">http://www.uscis.gov/files/form/i-485.pdf</a><br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;<a href="http://www.immigrationaide.com/forms/i601.pdf" target="_blank">http://www.immigrationaide.com/forms/i601.pdf</a><br />
<br />
下面是一个真实的例子。<br />
<br />
洛杉矶有位华人的父亲去上海领事馆签证，实话告知他是共产党员。结果拒签。他儿子说他笨，过了一阵再去签证，不再说自己是共产党员。居然签出来了。几年后麻烦来了，移民局说他故意隐瞒共产党员身份，要他限期离境。现在朋友正在花钱请律师在移民法庭打官司呢。<br />
<br />
 <br />
<br />
他的律师说，其实1990年的移民法令提供了如下几点豁免的途径。<br />
<br />
1、16岁以前加入共产党；<br />
2、加入共产党员属非自愿性质，或者说是被迫加入共产党；<br />
3、加入共产党时并非认同党的纲领与政治目标，而是为了改进物质生活<br />
4、在申请移民美国之前的五年里，已经退党（东欧国家的申请人为两年）；<br />
5、申请人的配偶、或子女、或父母、兄弟姐妹为美国公民，或其配偶、子女为美国永久居民。 <br />
<br />
尽管有这些豁免的途径，共产党员在申请移民美国时，一旦承认自己的共产党员身份，负责审理的美国领事官员依然不会签发移民签证，而会要求申请出具一份中英文对照的声明书，对以下的内容作一说明：<br />
 <br />
1、您所属于或被迫属于的共产党或其它恐怖党派的名称；<br />
2、您加入该党派的原因；<br />
3、成为该党派一份子的起止日期；<br />
4、所任职务；<br />
5、为何仍是该党派的一份子；<br />
6、成为该党派一份子后所得到的接收教育和生活水平方面的得益；<br />
7、您对该党派的组织结构、最终目标、处事方式、理论实践方面的接受程度；<br />
8、如果您已经退出了该组织的话，说明退出的时间以及理由。 <br />
<br />
在提出这份声明之后，美国领事馆会根据声明中的各项说明个别的作出是否能让申请人入境的决定。<br />
<br />
从上文可以看出，如果承认自己的共产党员身份，无疑会给申请人申请移民美国带来不小的困惑，而不承认自己的党员身份，不符合美国移民法的规定。</div>

							
							
							
							
