													<h2>许知远称韩寒的胜利是民族的失败</h2>
						
						
																											<div id="postmessage_609388" class="t_msgfont">许知远称韩寒的胜利是民族的失败<br />
(图) 2010年05月11日 09:37 华商报 <br />
韩寒入选时代周刊年度百人榜之后，突遭“炮轰”，知名作家许知远撰文《庸众的胜利》，网友麦田撰文《警惕韩寒》，分别对韩寒的“公共知识分子形象”进行批判，虽然网络的评论依然倒向韩寒，但对韩寒评价不再一边倒也让我们开始反思，对这个年轻人我们究竟了解有多少？<br />
<br />
批判一 韩寒需要警惕？<br />
<br />
观点：“韩寒根本没有独立思考，他所有的文章都是在迎合大众的情绪”。<br />
<br />
对于韩寒的争议，似乎还停留在新概念作文大赛的一夜成名、辍学等多年之前的事件上，但最近几年来，韩寒的形象俨然已经成为“公共知识分子”，在众多热点事件中，总是有韩寒的身影，虽然他基本不接受记者采访，但他的曝光率却居高不下，而且大多以正面形象出现。然而，5月7日一篇名为《警惕韩寒》的博文引起了对韩寒现象反思的开始，与过去不同，这次反思更多是对韩寒的批判。<br />
<br />
作者麦田称自己通读了韩寒的博文，并提出了韩寒博客的“2008”这个分水岭：在2008年之前，博客还是“私人习作”范畴，2008年之后，韩寒的博客重心明显转移到“公共话题”，尤其时政话题，“我个人一直比较欣赏韩寒。当我梳理韩寒全部博客时，我才发现自己的这个‘一直’，其实恰好也是从2008年开始。至少对我而言，韩寒是因为从2008年开始关注公共话题，才成为现在之韩寒；否则对我来说，韩寒和郭敬明并无高下之分。”麦田写道。<br />
<br />
在文章第二部分，他的主题为“露馅”，在列举韩寒几次博文的观点后，作者认为韩寒的话“不是理性的，但都是老百姓‘爱听’的”。<br />
<br />
因此作者得出结论，“韩寒的价值=永远的热点+一流的文字+正确的立场”，但是，“韩寒根本没有独立思考，他所有的文章都是在迎合大众的情绪”。<br />
<br />
批判二 韩寒的成功是民族的失败？<br />
<br />
观点：“韩寒掀起的迷狂，衬托出这个崛起大国的内在苍白、可悲、浅薄”。<br />
<br />
相比于麦田对韩寒抨击的浅显，知名专栏作家许知远所撰《庸众的胜利》更具力量与高度，他在更深层次分析韩寒的成功。“此刻的中国，人人都在谈论韩寒。他像是越来越压抑、越来越迷惘的时代的最后救命稻草。上了年纪的知识分子说他头脑清醒，媒体欢呼他是‘青年领袖’、‘年度人物’，青年人觉得他不仅很酷，还有思想。”<br />
<br />
许知远将韩寒描述为一个聪明的年轻人――“他的文章总是如此浅显直白，没有任何阅读障碍，也不会提到任何你不知道的知识；还有他嘲讽式的挑衅姿态，显得如此机智，他还熟知挑战的分寸，绝不真正越政治雷池一步；他也从来不暴露自己内心的焦灼与困惑，很酷。他能把赛车冠军、畅销书作家、叛逆小子和即兴讽刺者等多种角色结合，并能在种种诱惑面前保持警惕，况且他才二十七岁。人们尤其着迷于最后一点，他在自己全球浏览量第一的博客上，嘲讽这社会中的种种愚蠢和不公。”<br />
<br />
许知远的结论则是：“韩寒掀起的迷狂，衬托出这个崛起大国的内在苍白、可悲、浅薄――一个聪明的青年人说出了一些真话，他就让这个时代的神经震颤不已。与其说这是 韩 寒 的 胜利，不如说是庸众的胜利，或是整个民族的失败。”<br />
<br />
专家分析： <br />
<br />
越批判越清醒<br />
<br />
韩寒目前遭遇的质疑难免让人困惑。这究竟是真知灼见，是对他的洞悉，还是对他的误解。<br />
<br />
对韩寒遭遇到的批判，常年居住澳大利亚的历史研究者雪珥认为这很正常：“韩寒现象所承载的种种表现，以及这种表现后头的种种原因，归根到底只有一条：我们的时代的确是不同的，表扬与自我表扬者、批评与自我批评者，都能从中找到自己的镜子。这是一股能令人清醒的‘寒流’，它的力量绝对不来自韩寒，而来自你我，在此次批判中，人们会更清醒地看清韩寒。”<br />
<br />
雪珥这样分析，他认为，韩寒作为一个年轻人，靠说比较有技巧的真话赢得了民心，而不是靠装出精英状，这是时代的幸运，他并不是这个时代的英雄，也不象征着思想的力量，他只是象征着真实的力量，象征着那个指出皇帝的新装的孩子的力量，尽管涉及了很多敏感词，依然得到了主流的容忍和默许，这是时代的进步。<br />
<br />
如以往一样，韩寒对此次批判依然表现得不在乎，未作任何回应，雪珥说，这也值得其他批评者、尤其以批评为饭碗者思考。本组稿件由本报记者 狄蕊红 采写<br />
