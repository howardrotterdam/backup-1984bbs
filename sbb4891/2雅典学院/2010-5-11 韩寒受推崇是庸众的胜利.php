																											<div id="postmessage_609066" class="t_msgfont">知识与知识分子的贬值----漫谈网络业格局之七<br />
<a href="http://blog.sina.com.cn/s/blog_513a2b800100idx5.html" target="_blank">http://blog.sina.com.cn/s/blog_513a2b800100idx5.html</a><br />
<br />
近日，网上出现了一批人，以批判韩寒为己任，从思想，立场，动机和文风诸方面深挖狠贬，而且把韩寒热上升到了社会庸俗化，大众不可救药的高度。在这个题目上，一个叫许知远的所写的文字很可以一读。（<a href="http://www.douban.com/group/topic/11239596/" target="_blank">http://www.douban.com/group/topic/11239596/</a>）<br />
<br />
在这里我无意去批判许知远之流的言论，因为实在不值得认真一驳。以其立场之偏执，论点之耸动，论据之苍白，逻辑之混乱，都不是一般人可以理论的。喉舌们看不上韩寒情有可原，为什么一些远离名利场的知识分子们也看不上韩寒呢？上一次朝野文人们同仇敌忾，微言大义，以共同的气势却基于完全不同的理由共同贬低打击一个民间偶像的闹剧，想来想去只有二十多年前的王朔可比。王朔早已隐退，文坛却从此巨变；韩寒刚刚出道，什么坛深感危机呢？论坛？学坛？媒坛？由此想到更深层次的问题，一个孤独弱小的韩寒，怎么值得有些人如此大动肝火，上纲上线？原因无他，互联网是也。<br />
<br />
 <br />
<br />
几百年前，当以蒸汽机为标志的工业革命降临时，农业社会的价值体系随之崩溃。爵位贬值，宗教贬值，土地贬值，宗法家族制度贬值。反之，知识升值，教育升值，资本升值，武力升值。贬值者痛心疾首，大骂异类暴发户；升值者欢欣雀跃，嘲弄各种落魄人。从斯密，马克思，韦伯到狄更斯，巴尔扎克，都从不同角度或深刻或鲜活地解读了那个时代的矛盾与变迁。几十年前，当以互联网为标志的信息革命降临时，工业社会的价值体系随之崩溃。官位贬值，权威贬值，机器贬值，婚姻贬值。反之，人力升值，创新升值，技术升值，信息升值。和这里讨论的问题有关的是在信息社会里知识与知识分子贬值的现象。<br />
<br />
 <br />
<br />
所谓知识贬值至少表现在三个方面：1）由于互联网的普及，知识更新的速度加快，知识淘汰率大增，许多在工业社会有价值的知识和技能如今已经一文不值；2）由于互联网的普及，知识传播的数量大增，成本大减，表现为知识的价值降低；3）由于互联网的普及，绝大部分知识的表现形式更加通俗化，简单化，公开化，使得获取知识在相当程度上不再需要漫长的时间，专门的训练和特别的环境。与知识贬值同步的旧有知识传播方式和渠道的贬值，例如书籍，报刊，广播，电视，学校，一方面表现为价值的减少和市场占有率的降低，一方面表现为普遍的互联网化。<br />
<br />
 <br />
<br />
以此类推，既然知识和知识传播方式和渠道都在贬值，那么按照工业社会劳动分工，以知识的占有，传播和出售为己任的知识分子在信息社会里当然应当随之贬值。所谓知识分子贬值也至少表现在三个方面：1）由于互联网正在成为信息（包括知识）传播主渠道，原来由知识分子们垄断的旧有信息传播方式和渠道变成了整个信息传播平台的一部分，且日渐缩小，引起知识分子自身的市场价值降低；2）由于整个社会正在急速走向信息化，旧有的知识和知识形式在信息汪洋中所占比例日益缩小，新的知识生产和知识形式所体现出的大众化和互动化使得传统知识分子在其中所起到的作用在日益减少，引起知识分子的价值贬值；3）由于知识传播成本降低，大量非知识分子也在快速地知识化，白领正在成为社会的主要阶层，而旧日意义上的知识分子已经变成白领的一部分（教师，编辑，记者，作家，等等），更多地具有职业上的意义，而不再是社会阶层的概念。同工业化社会相比，无论从政治经济，还是从社会文化角度看，知识分子的影响力在削弱，社会价值在降低。<br />
<br />
 <br />
<br />
比较工业化革命和信息化革命，很容易看出知识分子的角色不同。在工业革命中，知识和知识分子是不断升值的，始终是社会变革的中坚力量，甚至在某时某地成为变革的领导者。但是在正在进行中的信息革命中，知识和知识分子是不断贬值的。从互联网发展的历史看，传统意义上的知识分子迄今为止不是这场变革的积极参与者和中坚力量，反而在相当程度上，成为了这场变革的受害者和革命对象。当然，知识和知识分子在信息社会中的价值依然存在，所谓贬值是相对工业化社会而言。但是，建立在工业革命基础上的知识创造，积累，传播体系正在被摧毁，知识分子们就整体而言在信息革命中是被动的，消极的，完全失去了变革推动者和引路人的雄姿。所以，我们到今天还没有看到能够说明信息社会的“财富论”，“天演论”，“资本论”，还没有看到能够描写信息社会的“双城记”，“悲惨世界”，“战争与和平”；看到的却是对以往那种学术传承有序，学科专门深奥，言论精雕细琢，传播主次分明的怀念和执着，看到的却是对互联网所带来的礼崩乐坏，庸众狂欢，俚语村言，竖子成名的深恶痛绝。这和大约一个世纪前，中国知识界面临西学东进，白话压倒文言时，林纾辜鸿铭们的立场何其相似。<br />
<br />
 <br />
<br />
我完全能够理解中国知识界和传播界中一些人对互联网引发的信息革命的隔膜与蔑视，因为被贬值了有牢骚是正常的。我也完全习惯了在一些报刊，讲堂，会议厅里听到互联网只是技术革命，网络传播低俗，政治精英，知识精英，传播精英们一定要占领阵地，引导潮流这类的梦呓，因为被边缘化了有反弹也是正常的。但我完全不能理解的是为什么今天有人忽作惊人之语，拿韩寒以及喜欢韩寒的粉丝们开刀。可以肯定，信息革命不是韩寒发动的，知识分子们被贬值与他无关。要骂就去骂谷歌，雅虎，FACEBOOK和TWITTER吧，因为它们也在把美国变成一个庸众社会（我倒是认为这是对信息社会的一个不错的称呼），顺便把中国也搞庸俗了。也许韩寒更多地在说这个社会不应该如何，这和他的学识经历有关，但这并不妨碍知识精英们说说这个社会应该如何啊。也许绝大多数中国网民更能欣赏韩寒的嬉笑怒骂，俚语村言，但这并不妨碍知识精英们言必称希腊，论必及罗马啊。也许韩寒目前的社会影响力高过若干精英们的总和，但这并不妨碍知识精英们改用老百姓听的懂的话在网络平台上同场竞技啊。如果我说因为麦当娜，LADY GAGA，老虎伍兹在美国粉丝众多就说美国是个庸众社会，恐怕知识精英们会有另一番说辞吧？简单按人头数量统计，全世界哪个国家不是庸众社会？要是一个国家人人都是人杰，那精英们的日子会更难过吧？韩寒现象无非表明在前网络时代，平头百姓没有言论传播的平台，而今天任何像韩寒这样的非精英也有了使用网络平台发言的权利而已。按任何理论，这都是社会进步的体现，为什么有人却耿耿于怀呢？所以，那些难以适应信息社会生活的旧日精英们与其把满腔郁闷发泄在韩寒与他的粉丝身上，不如静下心来，认真寻找自己在信息社会中的适当位置为好。既然贬值已定，不如换个角色，说不定还有再升值的机会呢。</div>

							
							
							
							
