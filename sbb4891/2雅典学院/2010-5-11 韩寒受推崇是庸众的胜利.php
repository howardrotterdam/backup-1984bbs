													<h2>韩寒受推崇是庸众的胜利</h2>
						
						
																											<div id="postmessage_609013" class="t_msgfont"><strong>来源新华网</strong>&nbsp;&nbsp;<a href="http://news.xinhuanet.com/book/2010-05/11/content_13477955.htm" target="_blank">http://news.xinhuanet.com/book/2010-05/11/content_13477955.htm</a><br />
<br />
约三十年前，当法国学生们被问道，谁是他们最仰慕的公众人物时，他们选择的不是老年萨特，而是米歇尔・科卢奇――一位电视喜剧演员，以嘲讽和出言不逊著称。<br />
<br />
&nbsp; &nbsp; 时代的风尚转变了，六十年代的理想主义、改变世界的愿望，让位于七十年代的幻灭与狭隘的个人主义。嘲讽成了时代的情绪，人们在乎的是姿态，而不是内容。<br />
<br />
&nbsp; &nbsp; 此刻的中国，人人都在谈论韩寒。他像是越来越压抑、越来越迷惘的时代的最后救命稻草。上了年纪的知识分子说他头脑清醒，媒体欢呼他是“青年领袖”、“年度人物”，青年人觉得他不仅很酷，还有思想。最近，<strong>韩寒被《时代》周刊评选为一百位影响世界的人物，xx宪章起草者、并为此付出十一年牢狱之灾的LXB倒是落选了。</strong><br />
<br />
&nbsp; &nbsp; 没人能否认韩寒的魅力。他能把赛车冠军、畅销书作家、叛逆小子和即兴讽刺者等多重的角色结合，并能在种种诱惑面前保持警惕，况且他才二十七岁。人们尤其着迷于最后一点，他在自己全球浏览量第一的博客上，嘲讽这社会中的种种愚蠢和不公――它们绝大多数与这个越来越膨胀和傲慢的官僚系统相关。有些时候，他不仅嘲讽，还期待创造意义，尽管他还不清楚这意义到底是什么。<br />
<br />
&nbsp; &nbsp; 作为一个青年人，这似乎已经足了，他必定是我们时代最可爱、最聪明的明星人物。但很多人(包括一些自认有思想的人)把他推到了一个令他本人都尴尬的位置――他要成为这个时代的英雄，象征着思想的力量，象征着对权力的反抗。<br />
<br />
&nbsp; &nbsp; 但这不是韩寒，人们越是把他推向这个位置，越暴露出这个时代、这些高声群罢叩挠薮馈⒋嗳跤肭优场Ｔ谀持忠庖迳希韩寒的胜利不是他个人的胜利，而是这个正在兴起的庸众时代的胜利。<br />
<br />
&nbsp; &nbsp; 是的，你可以说每个杰出人物必然与他身处的时代相关联，但一种越来越明显的趋势是――名声和有影响力越来越与个人的质量、才能与成就无关。在西方，这是个Paris Hilton和苏珊大妈的年代――她因为有名而有名，因为不怕出丑而有名。在中国，这是个李宇春与小沈阳的年代――人们因她小小的个性，或是他的自我贬损，而把票投给他们。<br />
<br />
&nbsp; &nbsp; 韩寒与他们不同，却也是被同样一种力量推到今天的舞台。他是这个时代明星文化与成功文化的产物，也符合这个时代所推崇的业余精神――赛车、写作、表演，你都要会一点；他还下意识响应了日趋烈的反智倾向，他的文章总是如此浅显直白，没有任何阅读障碍，也不会提到任何你不知道的知识；还有他嘲讽式的挑衅姿态，显得如此机智，他还熟知挑战的分寸，绝不真正越政治雷池一步；他也从来不暴露自己内心的焦灼与困惑，很酷……<br />
<br />
&nbsp; &nbsp; 于是谈论韩寒，变成了一次全方位的心理按摩。你沐浴了青春、酷、成功、机智、还觉得自己参与了一场反抗，同时又是如此安全，你不需要付出任何智力上、道德上的代价，也没有任何精神上的仿徨，他是这个社会最美妙的消费品。<br />
<br />
&nbsp; &nbsp; 但世上真有如此美事吗？用LXB来比较韩寒，既不恰当也不公平。但是，公众对两者的态度，却恰好不过的映衬了这个时代的特征。人们不谈论LXB，是因为他的名字不能出现在公共话语空间，也因为这有点危险。但集体性地沉默与忽视也在表明，其实我们对于真正的自由与反抗毫无兴趣，甚至心生恐惧。自由是需要付出代价的，它不仅要反抗，而且有明确的主张。这需要智力与情感上的成熟，并愿意为自己的决定承担后果。<br />
<br />
&nbsp; &nbsp; 对于韩寒的热烈推崇，是整个社会拒绝付出代价的标志。当我们沉浸于只言词组的嘲讽时，一定误以为自己已消解了这可恶的权力体制，其实一点没变，嘲讽只是为上面裹了一层糖衣，但我们进行自我麻醉，还将此视作一次反抗。<br />
<br />
&nbsp; &nbsp; 而且，人们或许也觉得，韩寒也不需要为行动承担任何后果，他可以进行象征性、边缘性的反抗了，然后还全身而退，像是去商场进行一次购物。韩寒成了所有人的借口，人们借着他撒娇，卖弄自己那可怜的“小心思”。<br />
<br />
&nbsp; &nbsp; 但公众必定为这种愚蠢和怯懦付出代价。既然他们对于真正的成就缺乏兴趣，不去赞叹那些卓绝的道德勇气，不去准备接纳真正的思想，他们就只能在这个烂泥塘中继续打转，相互抱怨、相互麻痹。因为这庸众的数量是如此的巨大，他们还会产生这样的错觉――中国已经影响世界。中国的确影响了世界，但它只是数量上的造就，而非真正值得尊敬的成就。韩寒掀起的迷狂，衬托出这个崛起大国的内在苍白、可悲、浅薄――一个聪明的青年人、说出了一些真话，他就让这个时代的神经震颤不已。与其说这是韩寒的胜利，不如说是庸众的胜利，或是整个民族的失败。<strong>（许知远）</strong></div>

							
							
							
							
