<br />
张鸣就认为许知远这篇《庸众的胜利》有太强烈的精英意识，完全无视韩寒身上体现的批判精神,认为只要拥韩,就是庸众。<br />
<br />
闾丘露薇则对麦田的批评不以为然，她在微博上写道，“人家就是做好自己，做自己力所能及的事情，这样简单不行吗？”而麦田针锋相对，称她这个观点相当糊涂，“当一个人成为公众人物时，怎么还能用他的个人行为作为规避批评的理由呢？”<br />
<br />
豆瓣网友“再见列宁”评论许知远的文章称：“为韩寒辩护的最糟糕的理由是嘲讽批评他的人是出于‘嫉妒’，就算批评者是嫉妒，说得对，也要听；这正如就算韩寒的动机是‘商业’，他说的真话也是真话。”<br />
<br />
而在新浪微博上，关于这两篇文章的讨论也甚为热闹，《东方企业家》杂志执行主编魏寒枫说：“我认真看过知远的文章。虽然他评价韩寒成名本质，某种程度也适合他自己；虽然他对韩寒和公众要求似乎过高；虽然他用‘庸众’一词略显刻薄；甚至虽然他评韩寒可能也有左宗棠不服曾国藩的情绪嫌疑――但你能否认，你不是一位‘庸众’吗？”</div>

							
							
							
							
