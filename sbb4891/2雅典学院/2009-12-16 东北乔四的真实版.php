&nbsp; &nbsp;&nbsp;&nbsp;黑道义士<br />
 　　十年前审理乔四一案的工作人员或调动或退休，我们颇费周折地找到了当年黑龙江省高法刑一庭副庭长李奎海，当时他是乔四一案的审判长。现已升任法院院长的李奎海回忆说，乔四一案中对李正光有所涉及，从案卷看李的罪行好象比乔还要重。但因为时间久远，具体的就记不清了。<br />
