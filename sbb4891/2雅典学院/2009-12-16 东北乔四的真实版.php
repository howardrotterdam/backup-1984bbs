&nbsp; &nbsp;&nbsp;&nbsp;其实四爷本来不姓乔，姓宋，名永佳。四爷小时候在家里排行老四，在他家的村头有一座桥。他每天早上都去桥上呆上一会（狠人都有怪癖），后来出来跑江湖不能把真实姓名透露出来，所以就化名成与桥谐音的：乔 姓了 ，这就是后来所说的乔四。至于四爷的真实姓名 很少有人知道。<br />
 　　关于四爷发迹的历史那就长了 要是说 估计比坏蛋那部书还要长，大概的一个过程就是他本人从一个包工头起家与各个道上的势力争斗，最后统一了黑龙江乃至整个东北的黑道，其实四爷不是象“谢文东”说的那样有勇无谋，如果四爷没头脑能一统黑道吗？能跟政界要员打交道吗，至于后来得罪中央某位领导人，那也是不知情的情况下得罪的<br />
