 　　突然，道外江边传来一声声撕心裂肺的惨叫，听来是那般凄厉。<br />
 　　原来这里演出的是一场&quot;黑吃黑&quot;的惨剧。陈建滨等几个流氓团伙的骨干，误以为他们绑架来的这个人在前几天刺伤了他们的成员，于是，就将其挟持到车上拉至道外江边，殴打威逼其供出同伙的住处。<br />
 　　他们砸开冰冻的江面，将其塞进冰窟窿里，冻他一会儿再拽出来，美其名曰&quot;涮冰棍&quot;。如此反复几次，那人已冻得不成样子，只剩下缩成一团瑟瑟发抖的份儿了。这样他们还不解恨，又举起猎枪用枪托朝其手指砸去，一根、二根、三根……直至十指鲜血淋淋，他们才将其塞进汽车里，拉至一所医院门口，像扔麻袋一样扔了下来，然后溜之大吉。<br />
 　　别看陈建滨也是独霸一方的人物，可他也有&quot;栽&quot;的时候。1988年，在哈尔滨松花江边的青年宫，陈建滨与李正光两伙人在此遭遇，因为买门票的小事结下宿怨。此后很长一段时间他们互相找对方的人打，双方都有死伤。<br />
 　　一天，李正光纠集了几个同伙，带上猎枪、刀、军刺等凶器，于凌晨摸到陈建滨的住处，不管三七二十一，照着炕上睡着的三个人一通乱砍，待砍完才发现弄错了，但他们并不就此罢休。后来李正光得知陈建滨要到哈尔滨市道里区顾乡赵庆凯家中参加婚礼，便指使陈洪光纠集一伙人携带猎枪，在途中对陈建滨进行堵截。当陈建滨行至道里区埃德蒙顿路大桥附近时，陈洪光等人将其打昏，后挟持到李正光家中。李正光<br />
<br />
 <br />
 、陈洪光等人又驾车将陈建滨挟持至香坊区哈尔滨自行车厂附近一机井房内，李正光持猎枪击中陈的右脚，又持刀将陈的左脚筋砍断，造成陈建滨右踝粉碎性骨折，身受重伤。<br />
 　　陈建滨当然不能就此罢休。伤愈出院后，一天夜间，他带领十几个喽罗一起出动，在一工地上找到了一起参与残害他的死敌袁新兰。在乒乒乓乓的枪声中，袁新兰抱头鼠窜，钻进基建工地，但终未逃脱陈建滨的魔爪。几个喽罗将袁新兰抓到陈建滨乘坐的面包车前，他吓得一头钻到车下，连声呼喊饶命。陈建滨端起猎枪冲着车下就是两枪，袁新兰当即毙命。<br />
 　　经查，这几个团伙共实施72起流氓犯罪，致伤82人。被伤害的人中除19人是流氓团伙成员外，其余均为无辜群众。他们所犯的罪行有流氓、盗窃、销赃、聚众赌博、伤害、妨碍公务、抢劫、绑架、行贿、贪污等，真可谓无恶不作，罄竹难书。就在&quot;乔四&quot;一伙自鸣得意、逞凶无忌之时，围歼黑势力的天罗地网已经布下。<br />
 　&nbsp;&nbsp;秘密调查　<br />
 　　1990年6月26日，哈尔滨市公安局处级干部彭兰江率已离退休老将为骨干的刑侦干部，对哈市的团伙犯罪开始进行全方位的调查，名称笼统而简洁--&quot;社会治安调查组&quot;。任务是，摸清犯罪团伙的情况，为彻底铲除这股恶势力提供强有力的证据。为不打草惊蛇，要求调查秘密进行。<br />
 　　从这天开始，调查组成员的身影开始出现在下属分局、劳改队、街道办事处等有关部门，仔细寻访，反复查证。但由于惧于这帮团伙的淫威，有的证人不敢作证，有时为取一证言需多次拜访证人，还需辅以耐心细致的思想工作。<br />
 　　一个多月过去了，犯罪团伙的脉络变得越来越清晰，就如鱼儿已经浮出水面，收网的时机就要到了。一个抓捕方案正在一次次修改并完成，确保万无一失。<br />
 　　1990年8月6日，市政府某会议室里，气氛紧张而又凝重，由傍晚到午夜，在严格保密的情况下，市领导和专案组的同志在推敲抓捕方案的每一个细节。<br />
 　　短兵相接，双方都感到形势严重。<br />
