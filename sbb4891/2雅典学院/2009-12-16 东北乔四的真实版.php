<br />
 <br />
&nbsp; &nbsp;&nbsp; &nbsp;l987年11月，郝瘸子从苏州往哈尔滨市批发鱼，在苏州居住期间与同去的一人发生争吵。为报复此人，他纠集了20余名同伙，分乘三辆出租车，手持大刀、铁棍、螺丝刀等凶器，到各处寻找这个人。当发现此人跑进一饭店后，他就指挥同伙冲进饭店大打出手。店主闻讯出来劝阻，竟被砍伤左臂。随后，他们将此人绑架到一大坝旁，不顾天寒地冻，扒掉他的衣服，用树条劈劈啪啪一顿抽打，直打到这个人钻进车底<br />
<br />
