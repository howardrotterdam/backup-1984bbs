&nbsp; &nbsp;&nbsp; &nbsp;李正光在哈尔滨人的记忆中整整消失了十年。<br />
 　　十年前的“8.10”之夜，操纵冰城黑道数年的“老大”乔四栽了，当地黑社会的25个总舵主中倾刻间19个落入法网，惟独他--号称乔四集团的“一号杀手”的他，神秘地消失了。<br />
