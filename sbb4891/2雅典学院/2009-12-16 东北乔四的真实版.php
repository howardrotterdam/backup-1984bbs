&nbsp; &nbsp;&nbsp;&nbsp;职业杀手<br />
 　　官方的统计数据说，以乔四为首的黑社会性质的犯罪集团三年间作案130多起。乔四靠威胁恫吓“钉子户”起家到发迹也就是那三年时间，李正光功不可没。<br />
 　　不管是黑吃黑、还是争霸逞强，没有李正光出头，精彩程度就要打折。一些知情人回忆说，每次恶斗都是李正光砍出第一刀，打出第一枪，冲在最前面。他是纯粹的职业杀手，动作麻利，弹无虚发，刀刀致命。他紧绷的嘴角，冷睃的目光很配他“第一杀手”的名号。<br />
 　　最让李正光大振声名的是李正光与小飞的那场恶斗。参与处理此案的宋平描述了打斗的前后经过：小飞原名叫陈建滨，是与杨馒头齐名的黑道人物，靠上百次的冲杀打斗在道上混出了名堂。有一年在哈尔滨松花江边的青年宫，李正光与小飞两伙人在此遭遇，因为买门票的小事结下宿怨。此后很长一段时间他们互相捉对方的人打，双方都有死有伤。最终的火并在李正光与小飞之间展开。枪、刀等等武器都派上了用场。在激战中，李正光用猎枪将小飞的右腿踝部击成粉碎性骨折，他又用刀片将小飞的左脚跟腱割断。小飞双脚被废掉，从此成了残废。<br />
 　　小飞的弟兄张晓光发誓要为小飞报仇，但他知道自己不是李正光的对手，只得将怒火发在李正光的同伙袁新兰身上，袁被其开枪打死。<br />
 　　此次李正光被抓获，公安部门评价其犯罪特征时说，他们分工明确，交叉作案，干完立刻就撤，具有很强的反侦察意识。这些特征无疑都得自李正光的影响。李正光作案多起，杀伤力极高，而且每次打斗、射杀都是速战速决，对头也不禁感叹他的“活”漂亮。<br />
&nbsp; &nbsp;&nbsp; &nbsp;同大多数道上人一样，李正光中途辍学，好勇斗狠，屡屡被公安机关收容教养。但道里区安宁街他的老街坊们告诉记者，这孩子十七、八岁时还挺本分的，很多同龄的孩子愿意找他踢足球、打篮球。二十岁左右他开始走上另一种生活道路。当李正光混成大“炮子”后，就很少回来了，人们看到的是更冷酷，也更实际。据说他不相信爱情那种虚无缈缈的东西--他一直单身，虽然身边不时也会有女人的身影。<br />
