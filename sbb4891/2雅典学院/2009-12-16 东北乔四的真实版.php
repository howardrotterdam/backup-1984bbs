 　　发迹<br />
 　　曾几何时，乔四还仅仅是一名小小的瓦工，但在工作中他发现政府的动迁、规划、基建等部门在城建中十分头疼&quot;钉子户&quot;，乔四瞅准了这个行当，网罗几十个弟兄，专门&quot;帮&quot;政府拔&quot;钉子户&quot;。凭借耍手腕、行贿和施暴，没过多久，即成了哈尔滨市龙华建筑工程公司副经理、龙华一工区主任。<br />
