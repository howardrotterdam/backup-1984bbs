而刘文彩的罪恶故事就是在那几年大饥荒年代中着手编造的。也正是在那个民众大量饿死的历史背景下，编造出来的刘文彩故事才拿来疯狂炒作，推向全国，推向世界。<br />
事实证明，现在刘文彩庄园里的所有展品依然全部都不是他家的东西，全是主管部门栽赃的，解说词也依然是极左年代用过的那些陈词滥调。<br />
看来王治安极不老实，在刘文彩故事已经破产的今天，他的《庄园秘闻》就是把秘闻的标签贴在过去的谎言上，妄图继续骗人。<br />
我的导游到此暂停，现在谈谈其他事情。<br />
先谈当年贫下中农诉苦的事情。<br />
公元一九六三六五年前后，主管部门组织了“家史演讲组”，即专业忆苦思甜班子。其中第一人就是冷月英。她刚出来讲述坐刘文彩水牢的故事时，很多本地人故意问她：我们怎么不知道刘文彩家有水牢呢？你是哪里来的，我们怎么不认识你呢？面对这些问话冷月英回答说：“我说的是冤枉话，但这不是我要讲的，是上面叫我来说的。”主管部门得知后十分不安，便立即通知她，除持组织部介绍信的来人外，对任何人不得泄露真相。从此冷月英就不敢再向别人讲老实话了。<br />
改革开放以后，在一次县政协会上，本地老人刘结熙故意问她坐水牢是怎么一回事，她回答：“不是我要讲的，是上面教我那样讲的，当时不那样讲也脱不到手！”可见冷月英是在压力下才扮演这一角色的。<br />
本地老人刘泽高讲，在本县调查水牢真伪时，冷月英对本地的许多老人讲，一个姓郝的干部（主管部门的人）找到她，要她把过去坐水牢的诉苦报告重说一遍，他用录音机录下来，然后放到庄园档案馆作为证据来阻挠水牢真象的调查，冷月英说：“我都没说”。她以此向大家表白，过去她所作的水牢报告并非她本人自愿，而是上级施压的结果。她现在不说了，证明她本人并不愿去冤枉刘文彩，证明她还是一个有良心的人。时代变了，社会进步了，冷月英才敢拒绝那帮人的纠缠，才敢拒绝她不愿充当的角色。<br />
前些年，王治安把持的《四川日报副刊》载文为冷月英“开脱”，说她出生贫苦，又历数她为党和人民作的种种好事，言下之意她是个好人，让大家原谅她。其实这是主管部门很狡猾的一招。表面上他们是为冷月英开脱，实则是把水牢谎言的责任全部推到冷月英一人身上，以便主管部门的那些策划者，教唆者逃之夭夭。《四川日报副刊》的文章接着又阴阳怪气地说当时他们搞错了，不是刘文彩关冷月英的水牢，而是刘文彩的侄儿刘伯华关冷月英的水牢（没听见冷月英这样说过）。<br />
刘伯华何许人也？刘伯华是刘文彩四哥刘文成的独生子，本名刘元舟，是个天生的痴儿。成人后的刘伯华也谈不上任何智商，他成天颠三倒四，满脸鼻涕口涎，专爱坐在地上。这是刘文成一生的痛心事，他常年把刘伯华关在后院，亲戚来了都不让看。原因很简单，一是怕别人看了笑话丢了自家的脸，二是怕刘伯华出门走丢了，更怕被坏人绑票，所以刘文成长年把刘伯华深锁在后院。如果按《四川日报副刊》说的那样，刘伯华带着狗腿子去抓冷月英，又把冷月英弄来吊起打，然后投入水牢，要是刘伯华有这等本事，其父刘文成不知会何等高兴。<br />
这就是王治安的“校正”。《四川日报副刊》登这样的文章，真是太无聊了！<br />
再谈“家史演讲组”的另一要员万洪云。万洪云原是刘文彩长兄刘文渊家的厨师，因做得一手家常菜而深得刘文渊的赏识。解放后，万红云有一次生病打针打漏了，便在手上留下一道凹痕。这本是一个医疗事故。当到了编造刘文彩故事的时候，主管部门就找到他，要他说手上的凹痕是当年刘文彩打的，并为这个故事专门设计制造了一条恐怖的钢丝皮鞭。万洪云从此就进入“家史演讲组”，并到全国各地去作血泪控诉，以他手上的凹痕和钢丝皮鞭为证据，来控诉刘文彩是怎样压迫贫下中农的。因此他成了仅次于冷月英的二号明星人物。<br />
冷月英和万红云的“血泪控诉”给阶级斗争增添了有血有肉的精彩内容，但接下来的事情又给阶级斗争赋予了绝妙的讽刺，这是经历过文化大革命的安仁镇人都知道的事情：<br />
文革中期两派斗争之际，万红云和冷月英各在一派。矛盾激化后就大字报相见。万红云率先写出大字报，揭露冷月英不是刘文彩的佃户，更没坐过水牢。冷月英等人也写出大字报揭露万洪云手上的凹痕是解放后打针打漏了留下的，根本不是刘文彩打的等等。这事让上面的主管部门非常尴尬，后来他们考虑到冷月英的影响更大更重要，决定挺她。于是就把万洪云驱逐出“家史演讲组”，又把《收租院》里万洪云挨打的塑像撤了。那条专为万洪云量身订制的钢丝皮鞭却留了下来，编成了另一个故事。<br />
主管部门向外宣称：居然让刘文彩家的厨师混进“家史演讲组”来了，可见阶级斗争之复杂，今后更要提高革命警惕，把好阶级斗争这一关。<br />
为了挽救被万洪云公开揭穿了的水牢故事，主管部门便把冷月英提升为县革委副主任，又以清查谣言为名来威胁恐吓本地民众，于是人们就不敢公开议论冷月英、万红云互揭老底的丑事了。<br />
再谈过去说刘文彩霸占贫下中农房屋的事情。<br />
公元一九四零年前后，刘文彩为刘文辉的反蒋大业而建“公益协进社”，联络各地实力派共同反蒋，刘文彩准备在镇上建一个小院来接待外客。他看中本镇乡民罗世维的房地，便与罗世维协商，刘文彩用N平街修的新房，而且多一倍的面积来与他交换。罗世维高兴地答应了。在罗全家迁入新居后刘文彩才拆了罗的旧房。刘文彩给建起的小院取名花园，这里成了他办公的地方。<br />
一九六三六五年前后，主管部门要编造刘文彩霸占穷人房屋的故事时，主管部门就找到罗世维做工作，教唆他加入“家史演讲组”出来诉苦。要知道加入“家史演讲组”就可以不下地干活，而且拿工资，还有很多特殊的待遇。可是罗世维坚决不干，一再拒绝。他说：“别人（刘文彩）让我搬出去住宽大的新房子，又是我愿意的，我有啥子苦诉嘛！”纠缠了罗世维好些日子的主管部门才去外乡另找了一个叫罗二娘的妇人来充当角色。开始罗二娘也不愿意，后来经不起威胁利诱才进了“家史演讲组”，成了第三号明星人物。<br />
除了这三人，“家史演讲组”还有几位成员，本地人都轻蔑地称他们是些不要脸的人，全是乱说胡说。本人认为不该怪罪他们，因为全是主管部门使的坏。<br />
关于贫下中农诉苦的内幕暂时讲到这里。<br />
下面讲一件无聊的事情。<br />
凡是地痞流氓骂街，都要施展他们下流的想象力，编造出无数污秽不堪的性脏话去侮辱对方。我们的主管部门也不例外。<br />
这些肮脏的故事有很多，下面选讲一例。<br />
过去地主庄园曾展出过这样一个故事：说刘文彩把一个川剧演员（罗某）关在家中，与自己的三儿子争相强奸，父子为此事争风吃醋，提枪对仗。此事在当年被各种媒体炒得沸沸扬扬。<br />
这个故事编出来后，主管部门才派人去新津川剧团找罗某，却始终没有去询问过“当事人”刘元富。本地老人刘泽高说：“这件事根本不可能，因为当时刘元富只有十几岁，完全是个娃娃，而罗某已经二十八岁了，又是一位袍哥大爷家养的情妇，刘文彩当时成天忙于各种事务，认都不认识她！”<br />
这是怎么一回事呢？本人为此采访过刘元富，现把调查的情况写在下面。<br />
刘文彩在他三儿子刘元富十岁那年，依照本地旧习为他订了娃娃亲。刘元富不愿意，小小年纪的他居然只身到成都去登报解除了婚约。刘文彩本是很守信用的人，与对方父亲又是朋友，所以坚持这门婚约。后来刘文辉回安仁镇，刘元富把此事告诉他，刘文辉很爱这个侄儿，就劝刘文彩，婚姻的事等后辈长大后自己作主。刘文彩对刘文辉的话没有不依从的，他就答应了。但刘文彩心里还是很不高兴，刘文辉走后，他生气地对刘元富说：“我今后再也不管你的事了！”<br />
刘家撕毁婚约在当地引起很大反响，加之俞家小组又是一个从一不二的旧式女子。乡长刘绍武的大小姐为此多次劝过刘元富，结果却是越劝越不听。大小姐就叫自己的丈夫黄某想办法。黄某说罗某走的地方多，见识广，找她出出主意。<br />
罗某何许人也？<br />
听镇上老人讲，罗是一个川剧艺人，当时是因事情请来镇上唱戏的，来的一共三位女角，一名叫玉霞，年龄最小；一名叫曲芬，长得最漂亮；还有一名就是罗某。她们三人来后就被三位袍哥大爷瓜分了。其中玉霞后来嫁与安雨晴作了小。三人中罗某年龄最大，容颜也较差，被张海如要了。张本是个花花大爷，丧妻后不再娶，专在外与妓女鬼混。他把罗某带回家不久，又带了一个年轻些的叫俞志坚的女子回家。罗某心里很不高兴但又不敢闹。罗某也有长处，她戏唱得很好，为人处事也很能讨人欢喜，安仁镇的小姐太太们都喜欢与她交往。<br />
当黄某找她想办法劝合刘元富和俞家小姐时，罗热心地说：“只要他俩常在一起就会产生感情”。黄某和其妻（刘绍武的大小姐）认为是好主意，于是就想办法安排他俩见面。一天，大小姐叫来俞家小姐，又叫人去请刘元富，说是来镇上打牌，地点在刘文彩叫作花园的小院内。<br />
刘元富来时一眼就看见有俞家小姐在坐，当即就想离开，但出于礼貌还是坐下了。在坐的四人是刘元富、俞家小姐，大小姐和罗某。刘元富应付了几转牌就起身告辞走了。<br />
事后，守花园的老头把此事告诉了刘文彩。刘听后非常生气，立即叫来儿子责问：“我给你订的亲你死活不从，现在给你退了婚你又背着我去与别人见面！……”刘元富回答父亲，不是他要去的，是大小姐叫他去的。刘文彩又叫来刘大小姐责问，大小姐推说是罗某的主意。刘文彩又叫来罗某，罗当时无话可说，硬着头皮挨了一顿骂，其间刘文彩叫她滚。第二天罗某就走了，从此再没有回来。事情就是这样。<br />
在上世纪六十年代，仅有冷月英、万红云、罗二娘等人出来诉苦还不够闹热，还要编出更精彩的故事。于是，主管部门的那些地痞流氓就把一件平常的家庭锁事编成了前面提到的刘文彩父子强奸罗某展开枪战的故事。决定让罗某进入“家史讲演组”，制造又一起轰动。<br />
主管部门的思路远不止此，他们考虑罗某是川剧演员脸皮厚，准备编出更多荒唐下流的故事，让她配合以亲历的方式讲出来。主管部门就是这样，他们先编好故事，然后再选中一个人，通过威胁利诱，让对方以亲历或亲眼所见的方式讲出来，这样刘文彩的故事就出笼了。<br />
主管部门于一九六六年三月派出一男一女（男的叫熊正甫，女的叫王俊华）到新津川剧团找罗某迫其就范。在政治强势的威胁和诱逼下，从未在庄园留宿过的罗某被迫说：“我在刘文彩家住了三年”。还说出林奶子、李二姐等人物和许多荒唐的情节来。本人问过刘泽高，他生气地说：“安仁镇从来就没听说有过这几个人物！”在诱逼下，罗某还说出她看见刘文彩的大门上挂着一颗血淋淋的人头。之后主管部门就在各种媒体上大肆宣传说当年刘文彩家门上还挂有血淋淋的人头。<br />
至于刘文彩父子强奸她的故事罗某无法启齿始终难以配合。世上怎能凭空逼迫一个女人说自己被别人强奸过呢？何况是一个未成年的娃娃强奸一个二十八岁的老娘们的荒唐故事！<br />
主管部门未达到目的，于同年四月又二访罗某。当时罗早已为人妻，并儿大女大了，她怎能按照主管部门的要求去讲那些无比肮脏下流的故事呢？在逼人的淫威下，在羞辱中，最后罗某跳楼自杀。<br />
罗某惨死后，主管部门立即在佛堂右边的女客房门口部置了刘文彩父子强奸罗某的现场对外展出，这真是对死者的莫大侮辱！ <br />
主管部门为什么处心积虑地要把这个肮脏故事栽在刘元富的头上呢？在此我要交待一下其中情由。<br />
长年在老公馆居住的刘文彩亲人中，当时活着的还有两个人，一个是五姨太王玉清，一个就是刘元富（杨夫人和其他子女一直住在成都）。他两人是庄园的活见证。主管部门作贼心虚，怕他两人站出来揭露他们的弥天大谎，为了封他两人的口，于是编了一个吃鸭蹼的故事栽在王玉清的头上；又编出上述故事栽在刘元富头上，使他两人背上这沉重的黑锅，自然就无力出来指控他们了。<br />
下面谈几件历史事件。<br />
刘文辉家族在起义投共前夕，为防止蒋介石的报复，于是他们把家中的贵重物品藏起来。刘文辉刚发布起义通电，蒋介石部队立即抄了刘文辉、刘文彩家族在成都所有的公馆，抢走了所有的家当，并四处追捕刘氏家族的成员。同时他们又派重兵去攻打安仁镇。在安仁镇，蒋介石军队又抢走了新老公馆的大量家当，包括刘文辉新公馆里的金库。<br />
主管部门在几十年的宣传中，每当提起刘氏家族因投共被蒋介石抄家这件事时，总是幸灾乐祸，极尽嘲讽之能事。在此我请问中共的党政领导，这是什么意思？<br />
主管部门在几十年的宣传中，每当提起刘氏家族为防备蒋介石抄家而转移财产之事时，总说成是为逃避共产党的没收。这是天大的笑话，如果真是这样，刘文辉家族还会来投共产党吗？<br />
<br />
下面谈谈刘文彩的杨夫人。<br />
自上世纪二十年代来，杨夫人便离开刘文彩带着孩子住在成都文庙后街。刘文彩病故后杨夫人成了一家之主。当时正是兵荒马乱之际，杨夫人准备带着子孙到海外去避乱世。她把此事告诉刘文辉，刘文辉劝她不要走，一起去投共产党，并要她装得没事一样，不要让蒋介石方面看出异样，紧要关头他会通知她撤离。杨夫人听从了刘文辉的劝告和安排。<br />
在刘文辉起义的当天上午，他派人来文庙后街通知杨夫人马上撤离。杨夫人当即跑到打金街姜亲家的家里躲藏。刘文辉通电起义后蒋介石部队就立即查抄了刘氏家族在成都的所有公馆，并四处追捕刘氏家族成员。杨夫人吓坏了，第三天天未亮，杨夫人带着儿媳张皇出逃。跟着一个引路人走田间小路，深夜才到达娘家大邑三岔乡，路程130华里。杨夫人是半小脚，兵荒马乱之际在外逃亡，当时的狼狈可以想象。<br />
1973年刘文彩的次子刘元华到北京去见么爸刘文辉，凤凰卫视制作的节目“大地主刘文彩”中刘元华讲述了当时的经过：刘元华到了之后，刘文辉就伤心地哭了，并挥手叫自己的子孙离开，只留下侄子刘元华在身边，所有的媒体只报道了这些情况，其实刘文辉留侄子刘元华在身边是要给他说一句话，这一句话原本只有三人知道：刘元华、刘元华的爱人和刘元华的儿子刘小飞。现我把它公之于众。刘文辉让自己的儿孙离开后，伤心地哭着对刘元华说：“老二，我对不起你们，早知道是这个样子，我就带你们走了！”<br />
刘文辉说的“我对不起你们”，我想可能就是指他当初劝杨夫人不要去海外，而要她随自己一起去投共产党，以至后来杨夫人一家都掉进了政治绞肉机里。刘文辉心痛啊！<br />
1951年，刘文辉因公从重庆到成都，主管部门在他到达之际，在劳动人民文化宫举办了“恶霸地主刘文彩”的展览。这是主管部门干的首次宣传活动，这是他们至今都经常提起的得意之作。可是，此次陈列的所有展品都不是刘文彩的，他的家当早被蒋介石抄得干干净净。这个展览其实是冲着刘文辉来的，为的是侮辱他，打他的威风。<br />
1955年西南军政委员会撤消，刘文辉被四川当局用花言巧语骗来四川成都，给了他一个省政协副主席的席位。在他到达之际，主管部门再次在成都文化宫举办“恶霸地主刘文彩”的宣传展览，其用心和目的再明白不过。<br />
主管部门疯狂迫害刘文辉家族，刘文辉住在成都的夫人李助乾也不能幸免。1965年四川当局把年逾六旬的李助乾强行押解到农村去监督劳动，听说还把她弄去游街批斗。刘文辉的长女刘元恺去找四川省委统战部，那里根本不予理睬（可见是故意的），直到刘文辉找了中央统战部后才把她放回来。身心受到伤害的李助乾于数月后郁郁死去。<br />
几年前我在《文摘周报》上看到一篇马识途的文章，说刘文彩在宜宾杀了共产党员李家勋和李筱文。为弄清情况，本人弄到了宜宾市志办公室出的《李家勋传》和《李筱文传》，仔细看后，这两人并非刘文彩所杀。刘文彩是税政长官，又不是城防司令、公安局长，抓人杀人不是他的工作。李家勋和李筱文二人的传记上写明是城防司令覃筱楼所杀。刘文彩与覃筱楼两人是众所周知的对头，覃的行为与刘文彩绝无关系。不过两人的传记上都写了刘文彩在宜宾的苛捐杂税繁多。这一点就得谈谈当时的历史背景了。那个年月正是四川军阀混战之际，为了打败对方，军阀们都在横征暴敛，竭泽而渔，这些军阀各多征了多少呢？以1935年为准，刘湘征到1975年，多征了40年；田颂尧征到1987年，多征了52年；杨森征到1989年，多征了54年；邓锡候征到1991年，多征了56年；刘存厚至少多征了100年；而刘文彩只征到1949年，多征了17年。如果列一张坏人排行榜，刘文彩名落孙山，最后一位。而刘文辉说：“刘文彩做的事该我负责！”<br />
下面谈刘文辉捐献财产之事。<br />
刘文辉早在1948年年底，就准备投共，而不是后来四川当局说的兵临城下的选择。刘文辉延迟一年多投共，那是中共中央从战略战术上考虑的结果。刘文辉听从中共中央的指挥。早在1948年以前，刘文辉就通过王少春问过中共中央，他投共后，中共将怎样对待他和他的家族（中共的做法其实是尽人皆知，只是看你相信不相信）。据我所知，中共至少对刘文辉说了：“土地上交，财产保留”这句话。王少春还给刘文辉讲了中共的统战政策。刘文辉是在这个前提下才最后决心跟定共产党的。才有后来他劝刘文彩的杨夫人不要去海外，而与他一起去投共产党的事情。<br />
真实的情况是怎样的呢？<br />
刘文辉起义成功，中共解放军一到就立即把刘文辉请到了重庆。随着国民党军的败走，解放军的到来，成都很快恢复了安全。刘文辉家族就陆续返回成都，他们无一例外地发现自己的家已被占领，刘氏家族通通被挡在街上不得进入。当时除了国民党的党产政产之外，其他军阀、官僚、地主们的财产都未被没收占领，没收他们的财产是后来土改运动的事情。刘氏家族的人员只好回到安仁老家，几乎同时西藏军区的解放军也住进了安仁的刘氏庄园，刘文辉的庄园住得最多，刘文彩的庄园住了一半。这些事情的发生都在国民党军刚败走，解放军刚到的那段时间，都远远早于土改运动。<br />
土改运动的事情在此我就不讲了（留在以后）。我只讲土改刚完，四川当局得知刘文辉手中还剩有一笔钱，于是就又向刘文彩的杨夫人及刘文辉的其他哥哥那里逼要，说是剥削的罚款，数目与刘文辉手中的那笔钱刚好相同。逼要的手段与土改的手段一样惨烈。刘文辉是明白人，为了救自己的亲人，他把剩下的钱乖乖地全部“捐献”出来了。从此刘文辉成了无产阶级，他住的是公房，房中的家具也都是公产，每月领取400余元工资，直到1976年逝世。当年刘文辉对他的老朋友们这样讲过：“我对他们（西南军政委员会的领导）讲，钱我不要了，我全部交出来，我们刘氏家族是诗书礼仪之家，就爱这点面子，这点面子你们总该给我嘛！”<br />
刘文辉爱面子比爱钱都更爱，他的弱点为对方知道了，我估计这就是后来编造刘文彩故事的发端。<br />
刘文辉的儿子亲口对刘小飞说过：“如果上面没人点头，他们（马识途、李维嘉、周鼎文等人）是不敢整我们的”。上面的那个人我知道是谁，这个人不是刘伯承，不是邓小平，这个人当年听信了马识途的谗言，自己后来在文革中也为谗言所伤，最终丢了性命。<br />
迫害刘文辉家族的人中有一个是李维嘉，解放前李维嘉在成都身份暴露，危急之际是刘文彩最亲近的侄子刘元u救了他，把他藏在家里，还把他送到雅安保护起来，同时给了他许多钱。当时李维嘉把刘元u介绍加入了共产党。解放后李维嘉翻脸不认帐，刘元u一再给自己的家人说：“李维嘉这个人太不要脸了！”1996年刘元u逝世，李维嘉是同一层楼的近邻，却不去看他的救命恩人，刘元u的后人都说他不要脸！<br />
下面谈谈狗腿子的事情。<br />
刘文彩家中从来就没有宣传中说的那种狗腿子。他家只有四十来个雇工，这些人都是贫雇农，他们平时里种菜，管理果园，打扫卫生，处理粮食，秋收时收租谷，空闲时在庄园里打打牌，从未在外伤害过他人。本地老农对我说：“主人都是善人，下面的人也学主人的样。”在土改时，因刘文彩与本地农民有着极深厚的友情，所以不敢在大邑安仁当着本地民众的面迫害刘文彩的家人，而是把他们弄到成都慈惠堂街七县联合办事处关起门来用刑。刘在安仁的管家薛筹九连批斗都没受过。薛筹九是在农业合作化时被逮捕的，因为他当时雇了几个人干活，罪名是说他破坏农业合作化。<br />
宣传中的狗腿子不是指上面那些介入了刘文彩田产经营的那些人，而是“公益协进社”中的人。“公益协进社”是袍哥组织，那些是追随刘文彩反蒋的人，这些人从未介入过刘文彩的田产经营。是主管部门出于邪恶的政治目的故意混淆历史，把这帮人说成是“收租院”中的狗腿子。<br />
下面我来讲几个“狗腿子”（公益协进社中的人）的情况。<br />
一、李育滋是安仁镇的名门望族，他是刘文彩在政治上最忠实的追随者，李育滋一直保护和救助中共地下党李维嘉、李安澜、周鼎文等人，解放后这些人不仅不报答，反而咬他一口，说李育滋是大地主、大恶霸，镇压共产党，并把他投到牢里施以酷刑，先是捆住他的两个大拇指反吊起来毒打，当手臂吊断后又强迫他跪瓦砾，并用步枪上的钢条把他的左眼球活活挑出来，后来枪毙李育滋时周鼎文还高高坐在审判席上（解放后他任大邑县法院院长），李育滋死后尸体拿来示众，其间他的心脏、肝脏被挖，连生殖器都被挖了。<br />
二、刘绍武是本地乡长，当年国民党通缉周鼎文时，是刘文彩收留了他。刘文彩把周安排到刘绍武家当家庭教师，要刘绍武保护他。刘绍武就供他吃，给他钱，一直把周鼎文保护起来。土改时周鼎文把刘绍武定为恶霸地主，还说他是反共救国军而把他枪毙了，连他的大小姐都被周鼎文逼得自杀了。<br />
三、起义人员刘结熙告诉我，他的父亲刘从周（“公益协时社”的副总社长）本是去解救中共地下党，解放后这些人反咬一口说他是去镇压他们，还编出一些情节写进伪历史中。<br />
四、刘泽高是“公益协进社”的管事，与刘文彩的田产经营并无关系，但现在成都的媒体都说他是刘文彩管帐的管家，看来还在混淆历史。<br />
五、住在安仁裕民街35号现在还活着的杨茂全，他是刘文彩的亲信，“公益协进社”的三牌管事，全家仅两亩地，是个典型的贫农。<br />
王蕴兹的起义部队得以逃脱国民党的追杀，杨茂全是立了汗马功劳的。下面是刘元富告诉我的那段历史。<br />
1949年5月，民革地下党组织川西地区主要负责人王蕴兹（崇宁县参议长）领导的地下武装4千余人（大多是袍哥武装和争取过来的部分警察），因过早暴露，仓促提前起义，为王陵基的保安部队击溃，王蕴兹和另外几位领导人刘绍成（崇宁县参议员）、伍锡青（崇宁县袍哥组织总社长）、刘本元等带领余众百余人，欲往雅安依靠刘文辉，但沿途关卡盘查，势难飞渡，就昼伏夜行去到安仁镇，要求公益协进社支援，这两方组织过去虽无联系，但都是刘文辉掌握的地方力量，自是一谈即合。当时，刘文彩已去成都养病，行前委托李育兹（开明士绅）代总社长，处理日常事务，李见事关重大，并且一下子来了近千人（随同王等来的百余人，后又陆续到达七、八百人），供给问题也不是日常经费能够负担的，于是一面安置来人，并给刘文彩写信请示办法，又恐文彩病重不能烦神，另给其兄李光普（西康财政厅长）去信，请其转禀主席（刘文辉），两信交给总社三牌管事杨茂全火速送到成都。杨茂全先去见刘文彩，文彩在病床上看了信后，闭目细听杨茂全详述情况，随即命人给其老家总管杨孟高写信，嘱其全力解决来人所需费用，并命杨茂全立即去西胜街李光普公馆，李见信即带杨坐上小车，直驶新玉沙街刘文辉公馆，将杨留在副官处，自去见主席。刘文辉考虑，此事不宜动用军队，只能使用地方力量，但文彩病重，找谁好呢？还费思量中，猛然想起，过去曾任24军某师参谋长的张鹤龄去信，说安仁镇要送客人到雅安，请张与王陵基驻三岔乡的保安部队邱营长打个招呼，再由李光普给驻邛崃的24军谢海凡营长写信，嘱找一辆卡车，准备送人到雅安，然后李光普给李育兹回信，并转致来客，去雅安宜隐蔽，不宜多人，以免招惹蒋特耳目（这会过早暴露24军的政治立场）。杨茂全回到安仁镇，拿到刘升廷给张鹤龄的信后，又赶赴邛崃送去。张鹤龄见是老厅长（民国七、八年时，刘升廷曾任四川省检察厅长，时人多以老厅长称之）的委托，不敢怠慢，即同杨茂全到三岔乡会见邱某，说安仁镇有客人去邛崃，经过贵军驻地，希望不要误会等语。邱某这个营，就是王陵基安在成雅小道上的卡子，负有防范革命进步人士通往雅安的任务，（当时雅安是出了名的民主人士之家）今见大脑壳当面来说，心知事不简单，脑子里飞速运转，想到眼前这个张某就得罪不起，如果报告上级，都有公益协进社的堂口，20里外的邛崃就有24军驻扎，如真得罪了安仁镇的人，自己不会有好结果的，不如卖个人情，今后在地方上也好相处，打定了主意，满脸堆笑，忙说：“小事一桩，张老派个人来说一声就是了，何必亲劳贵步”，张老干笑一声：“嘿嘿，好说，恐怕叫人来说不清楚，反转误事，顺便介绍这位杨三哥给你见见面，日后就是由他陪客人一路的”。邱某连说：“没问题，没问题”。这一关打通了，杨茂全陪张鹤龄返回邛崃，再去谢营长处交了李光普的信，同谢约好上车日期，才回到安仁镇向李育兹汇报。（以上是刘元富提供的史实）<br />
王蕴滋的队伍最终到达了刘文辉的驻地，终于得到了保全。杨茂全功不可没。<br />
解放后周鼎文把杨茂全定为狗腿子地主，判刑两年。<br />
“公益协进社”本是刘文彩在刘文辉授意下组建起来的反蒋组织，后来被周鼎文等人以种种阴谋手段把它说成了反共组织，这些情况本人在以后专文讲述。<br />
下面我来谈谈大邑县人眼里的刘文彩和主管部门干的一些事情。<br />
刘泽高说：刘文彩一回到家乡便鼓励乡亲经商致富。当时安仁镇只有一家菜油作坊，他问老板有多少库存，老板说有二十大缸，每缸两百斤。刘文彩当即全部把油买下，之后油价就涨了，他又把这四千斤油全部白白送给了油房老板。刘文彩说：“我就是要奖励那些经商致富的人”。以此给乡民灌输商品意识，致富意识。<br />
一九九二年小平同志南巡以后，大邑县县委统计历史上谁给大邑县作的贡献最大，统计的结果有两个人，第一个就是刘文彩。主管部门得知后惊恐万状，他们跑到县里来大吵大闹，强令本县不准将此事以文字形式写进档案。十多年来主管部门四处指责为刘文彩“翻案”的种种事件，但大邑县县委统计的事他们却从来不敢提起，生怕张扬出去。<br />
上世纪九十年代，安仁镇党委书记李嘉宾在一次会上发言：“当年刘文彩发展了安仁镇，现在我也要发展安仁镇……”<br />
一九九五年三月份，原文彩中学五十周年校庆之际，时任县长的金嘉祥先生顺应民意，提写了“文彩写春秋”，主管部门为此事到大邑去与金县长大吵大闹，说他为刘文彩翻案，现在都没放过他。<br />
在此我要问主管部门，刘文彩有案吗？刘文彩的案在哪里？我们看见的只是铺天盖地的泼妇骂街式的宣传，难道这些就是刘文彩的案吗？<br />
一九九五年刘小飞和他的友人一起到安仁镇去，亲自听见庄园工作人员杨正先生说，他考证了那段历史，刘文彩回到家乡做的全部都是好事。他还讲了一件事情：当年一个很穷的农民在走投无路时来找刘文彩，刘文彩亲自出来接见他。农民说他穷得年都过不了，刘文彩立即叫家人拿出很多年货送给他，这些年货足够他们全家好好过一个年。临走时刘文彩对那位农民说：“你这样子还是不行，等你把年过完了再来找我，我从新给你想办法”。这就是刘文彩的为人。<br />
一九九五年本地企业家闵德宁捐资三十多万装修了原文彩中学的大门，并在校园里修建了一座三十余米高的蜡烛形纪念碑，上书“百年树人”。他在会上说：“过去说刘文彩的那些事，我们本地人都不知道有那些事；过去弄我们去政治学习，我们又懂不起；我们只相信自家老人告诉我们的：刘文彩是个好人。所以我捐资修建这个纪念碑，象征刘文彩的光辉照耀家乡！”闵德宁的这些话在安仁镇是有代表性的。主管部门恨死了闵德宁，却不敢公开提起这件事，但八年后他们还是以种种借口拆了这个纪念碑。<br />
2004年刘小飞再次和友人到安仁去，在街上他们故意问一位三轮车夫：“刘文彩是不是恶霸地主？”对方很生气地说：“我们懂不起啥子叫做恶霸！”这时旁边一个叫廖福全的三轮车夫说：“刘文彩的事情我们不知道，我只听见我的老人（父亲）告诉我，当年他捡狗屎路过刘文彩家门口的时候，刘文彩看见了就请他进去吃饭。”过去的宣传却编造说，刘文彩专放狗去咬那些讨饭的人。这里我要指出，刘文彩最爱干净，他嫌狗脏，终其一生家里从不养狗。<br />
听本地人讲，刘文彩生活简朴，他衣着朴素，饮食简单，长年只吃三样菜，一是回锅肉，二是煮回锅肉的汤加青菜，三是时令蔬菜。而主管部门却大肆宣传说刘文彩专吃人奶，并用了六个奶妈。这等充满腥气的故事，只有禽兽才想得出来。过去的宣传还说刘文彩的三太太嫌包车硬，弄一个胖女人来做肉垫。三太太又不是同性恋者，她怎么会坐到一个肉感十足的胖女人怀中去呢？编这种故事的人只能是变态狂。<br />
我还听到本地人说，当年刘文彩不会眼见自己的乡亲到外面去讨口，凡有困难的人家他都要帮助，凡向他求助的人没有一个是空手而归的。刘文彩在时，他的家乡没有一个人到外面去要饭的。即使极左年代在主管部门的淫威下，本地的贫下中农在私下还是众口一词地说：刘文彩在的时候我们都有饭吃！都吃饱了的！在安仁镇刘文彩的口碑是最好的。<br />
庄园负责人吴宏远也承认，过去贫下中农家里有人生病，刘文彩知道后都送医送药。<br />
安仁镇所有的民众对泥巴捏的《收租院》都嗤之以鼻，他们说：当年在刘文彩家交租时，收租现场摆了好几张饭桌，交租的人凑足八个人就摆饭，上的菜全是大盘大碗的回锅肉、红烧肉等，剩下的还可端回去给婆娘娃娃吃。好些人为了来此多吃一顿，就把一次可以交完的谷子分两次交。难怪他们说：“编他妈Pi个啥子《收租院》出来！”<br />
2003年前后，隆昌石油井下作业处的野外队（固井队和压裂队）多次出差路过安仁，其中固井队十余号人在安仁周边的农村走访了二十余家农户，他们都说刘文彩对他们很好，并列举了很多当年刘文彩做的种种好事。有人反问那些农民：“你们说他那样好，他为什么要收你们的租呢？”农民们回答：“别个（刘文彩）用自己的钱来买的田，咋个不该收租呢？别个是怎样收的租呢？穷人不交租，他问都不会问，像我们这些交得起的不交也不会来问，凡是去交租的，他就大油大肉的请我们吃一顿，吃不完的还可以端回去给娃娃吃。”压裂队的出差人员比固井队的人员还要多，他们走的是街上，街上的人对他们说刘文彩为人很好，在列举了他生前作的种种好事后说：他是大家公认的刘大善人。<br />
近日，我到安仁镇瓦窑村五组见到刘文彩当年的家庭厨师刘玉林老人（现九十岁），他说当年不敢说刘文彩好，要不他们（主管部门）就要整他，还要打他。现在他敢说了，刘文彩对人很好，遇见他（指刘文彩）家忙的时候，刘文彩就下厨来对他说：切厚点，没关系。他还说，刘文彩没吃过啥子，都是一般的。又说帮刘文彩两年，他怎么做刘文彩就怎么吃，从没说过他。在场的二十几位乡邻都说刘文彩造福乡里，当年每到年关，他都在兴福寺施米接济穷人，此活动长达两三天，直到一仓米施完为止。其中一位叫李福全的乡民说，他母亲告诉他：当年一个叫刘平山的鸦片鬼抢占他们的房子，他母亲就去找刘文彩，刘文彩说：你搬回去，看哪个敢来撵你走。他们就搬回去了，从此对方再不敢来欺压他们。<br />
本地人还说：说刘文彩过去在宜宾怎样怎样坏，那些事不可信，他回家乡十二年，他的为人和所作所为是有目共睹的，一个人的本性是不可能伪装那样久的。刘小飞在石油固井队时，有个姓孙的同事，他对刘小飞说：他的大爷爷孙明高曾是当年刘文彩在宜宾时的厨师，在上世纪六十年代抹黑刘文彩的岁月中，他一再对孙家的人说：那是乱说的，刘文彩对人好得很。孙明高解放后成了宜宾国营餐饮界的一代名厨。<br />
<br />
当年主管部门宣传说刘文彩修庄园霸占农户的田地房地，害得别人家破人亡，而庄园后面94岁的老贫农刘丙南说，当年刘文彩占用了别人的房地田地，就修一个四合头的房子还对方，还另送8亩田（有的送15亩）。真是好价钱！<br />
2007年3月份，本人到安仁去采访，一位县人大代表对我说：每次开人大会时，都有安仁的代表出来振臂高呼，要为刘文彩正名！可是县里不敢表态，更不敢受理。<br />
1964年，以真人真事不必要（即无中生有）编造出来的刘文彩故事即将推向全国之际，主管部门做贼心虚害怕本地民众揭露他们的弥天大谎，他们就策划把民众打压下去。这个工作首先在刘文彩的近邻中展开，因为这些人最了解情况。<br />
这些人中有一个叫刘福田，是个贫农，刘文彩死时他35岁，当年他与刘文彩关系很好，又曾在刘文辉手下当过连长，依据这些背景，怀疑他会向外界讲真话，于是决定用他来开刀，然后开展暴力驱逐庄园周边十余户农民的工作。<br />
主管部门以刘福田是刘文辉手下的伪军官为借口把他捆走，由民兵头头郑泽安把他反吊起来毒打，又绑到各种会上去批斗，然后把他定为反革命份子。整刘福田就是为了制造恐怖，威吓民众。果然，民众吓住了。<br />
当年10月1日本地开大会，以刘福田是刘文辉手下的伪军官的罪名宣布逮捕，并判无期徒刑（杀人灭口）。接着又宣布对庄园周边十几户农民的驱逐令，驱逐令上规定没收他们的房子和所有家具等。工作必须当天完成。<br />
逮捕刘福田已经把人们吓住了，接下来的驱逐令更是把人们惊得目瞪口呆，他们说：我们没有犯过任何法，没做错过任何事呀！无辜是没有用的，因为这是主管部门的决定。<br />
这些人被发配到好几里路以外，又不作安置，他们只好去住破败的茅棚、窝棚，里面腐臭难当，没有床，更没有家具。茅棚矮得腰都直不起。所有被驱逐者都被定为“坏人”，在新地方他们被歧视、被修理，还克扣工分、克扣口粮，简直把人往死路上赶。<br />
在极左年代，只听说把 “有问题”的人押解回原藉劳动改造，还没听说过从原藉发配他乡。<br />
刘福田的大儿子刘德均一年前就与其父分家，此时也被发配。他的妻子邓秀芳马上就要生产了，因发配地床都没有，哀求下宽限了几天，就这样还隔三差五来催促，阴历九月二十五日，极度惊恐和紧张的邓秀芳终于产下了孩子。接着上面就来催逼，生产不到十天的产妇就这样被赶出了自己的家。近日我到五星六队去看望他们，好远一段路！当年真不知一个产妇是怎样走过去的。<br />
刘福田的妻子拖着另三个孩子，其艰难无法言说。子女不准报名读书，刚满三岁的小儿子送给了人家。这就是一家贫农的悲惨遭遇。<br />
1979年，由于邓小平上台中国的政治发生了很大的变化。刘福田才以量刑过重放了出来，当时他已劳改了十五年。量刑过重不等于没有罪，现在都没平反。可是，正因为刘福田是刘文辉手下的“伪军官”，上世纪八十年代成都军区发给他起义证书，民政局每月发给他500元的津贴。同一个原因，主管部门判为罪，成都军区定为功，真是天大的怪事！<br />
当年二十八岁的刘世柄也是被驱逐者。他毕业于新津师范学校，当过教师，其妻廖秀芳是共青团员。刘世柄的父亲是1960年饿死的。刘世柄认为自己的房子是土改分的，自己又没有犯过任何法规，凭什么没收自己的房子和家具。他找上面评理，根本没有用。回来时他的房门已上了封条。刘世柄见状气愤已极，上前把封条扯了，夫妻双双进门回到自己家中。上面知道后，就把枪发给了打手郑泽安等人，当天晚上刘世柄有重大危机！好在有人把险情告诉了他，他吓得立刻跑到外县去了。<br />
晚上民兵来的时候，家里只有刘世柄的妻子共青团员廖秀芳，她的背上还背着一个不到四个月的儿子。民兵们立刻上前把她按倒在地，用绳子把她五花大绑起来，押往发配地。押解途中路过桤木河大桥时，廖秀芳由于气愤和绝望，背着孩子就往河里跳，好在被人拖了回来。由于发配地的破茅棚没有门，刘世柄便返回自己家中下了一扇门来挡风雨，为此，上面扣了他四百斤谷子，那是他全年的口粮。<br />
发配的名单中有一个五十余岁的寡妇廖素瑶，其夫死于1958年的大跃进。因她三十几岁才嫁人，所以在扫地出门时两个女儿才分别十五岁和十七岁。这个家庭本来就过得十分艰难，上面也不放过他们。母女三人被发配到十里外的新河，途中要踩过一段水路，十五岁的小女儿刘世伦踩水时不慎扑到水里，那时她正来月经，到了新河就病了。当时吃的住的都没有哪来钱治病？由于病，由于境况恶劣，更由于野蛮的政治迫害，一个月以后她就死了。花儿还没有开放就已经凋零。<br />
还有一个外来户陈履章，当过民办教师，其妻刘世英出身贫农，他们也被发配。这家人走得最晚，是这年阴历10月23日才被扫地出门的。走时儿子陈贵伟未满三岁。<br />
小贵伟原本长得很胖，他看到自己的父母被人用暴力驱赶，受到了惊吓。新环境的恶劣又增加了他的恐惧，他天天哭喊着：“我要回去！”没几天他就病了，当时一家人分文没有，直到孩子病重了才借了十元钱到县医院去。医生说要住院，要60多元，陈履章夫妇没有办法，又把孩子抱回来。小贵伟是当年腊月26满的三岁，腊月28就死了。死前他一直哭喊着：“我要回去！”小贵伟死后不久，刘世英就因劳累过度从此成了残废。<br />
在刘文彩与刘文辉两个公馆之间，住着爱国抗日将领刘湘么爸刘文礼的后人，上面当然不会放过他们。在下驱逐令之前，刘文礼的小儿子刘元金就被抓到县里关起来。在找不到任何罪名的情况下，把他定为“坏份子”，并把他捆起来四处批斗。刘文礼有个孙子刘世海，1977年借口都没要就把他抓走了，直接关进高山劳教农场。三年后，由于邓小平出来工作了，中国的政治有了很大的变化，刘世海才被放出来。出来时已被逼疯了。疯后的刘世海每天到街上嚷嚷，上面害怕暴露他们害人的罪行，在一天夜里派了一辆城管的巡逻车把他拉走。从此刘世海就在安仁民众的眼中消失了。时间过去二十多年，至今活不见人死不见尸。<br />
在此次名为纯洁庄园周边阶级队伍的行动中，主管部门的打手刘世应等人还扬言要把这些人统统杀了，在恐怖下有些单身青年就吓得远走他乡。如陈建中和刘世伟等。<br />
当把这些不放心的人发配走以后，还是发生了一件事情。支部书记的父亲老贫农刘月斌在回答外来参观者的问题时说：刘文彩家没有水牢，那是刘文彩放贵重物品的地方。主管部门知道后把支部书记叫去训了一顿，他回来对自己的父亲说：“以后如果外人再问你，你就说不知道嘛，就不要再说真话了。”<br />
当年，这场血腥的迫害只是开始，接着这一工作就全面展开了。他们逮捕了不少知情者和敢讲真话的人，抓来游街批斗的人就更多了。<br />
在此讲一件事情。上世纪九十年代有个现役军官周东浩，他写了一本《地主庄园沧桑录》，该书虽然是帮主管部门说话的，但他还是讲出了一件血淋淋的事件：“大邑县唐场乡曾是刘文彩控制的地盘。在那里，有一张姓人家，父辈与刘文彩关系交好。张家唯一的儿子张某长大后，刘文彩做媒将安仁一位姓刘的姑娘嫁给了他。<br />
……文革一开始，村里便首先将已40多岁的张某揪出来批斗，原因当然是张某与刘某的结合乃是刘文彩做的媒。在一次批斗中，张某实在忍无可忍，便问了一句：“我们犯了什么法？”愤怒的批斗者们听了这话，好似火上添油，大骂张某“死不改悔”，随之将张家的房子拆毁，将张某的妻子一同绑来批斗。不久，张某病故（病故之说不可信。嘲川），丢下其妻继续遥遥无期的“戴高帽”批斗。一次，张妻正跪在台上接受“深揭狠批”，突然想到丈夫已惨死，房屋又被拆，自己无依无靠，便忍不住伤心地放声大哭起来。有人便说她这是对批斗的一种反抗，于是，砍来酒杯粗的带刺木棍抽打她，一夜下来，张妻浑身血淋淋的动弹不得……”（《地主庄园沧桑录》188页）<br />
本地民众就是这样被镇压下去的。纵然如此，当年面对主管部门的淫威，本地民众还是公开表示刘文彩的那些事情他们不知道，以此来抵制和否定主管部门的宣传。刘文彩慷慨好义的豪侠形象已深深植入安仁民众的心中，谁也抹煞不掉！<br />
1960年前后，就在主管部门花重金装修刘文彩的“逍遥宫”的时候；就在主管部门花重金购买金银珠宝来充实刘文彩庄园的时候；就在主管部门用3万斤大米打造一张龙床来栽在刘文彩头上的时候，庄园后面的贫下中农正在饥饿的生死线上挣扎。现年90岁的老贫农廖桂英当年全家15口人就饿死了一半，他的大伯子刘元安一家5口饿死光了。老贫农刘丙南一家死得最少，死了2个人。<br />
讲述到这里，到底谁祸害地方，祸害民众，谁在使用打手狗腿子，谁是真正的恶霸不是清楚了吗！<br />
在本世纪2004年3月份的一天上午，在安仁镇刘文辉新公馆里发生了一件有趣的事。当今主管部门负责庄园宣传的要人王治安与刘文彩的孙子刘小飞不期而遇。他两人有好些精彩的谈话（可惜刘小飞当时不清楚对方的身份，要不这次谈话将会有更多精彩的内容），在此我只讲其中一段：<br />
刘小飞说：“宣传中说我爷爷剥削。我爷爷回家乡十二年间收的全部租拿去修文彩中学，一半都修不起，还不用说他修成都到大邑的公路，把一条街修成七条街。这是剥削吗，还是奉献？”刘小飞没有讲出的是：刘文彩组建“公益协进社”反蒋用的钱就更多了。<br />
王治安没有下面回答，而是转过脸去自言自语道：“所以这件事有必要再控制五十年，到那时知道这些事的人就少了，就不会去说三道四了。”<br />
由此可见，刘文彩这桩公案主管部门拒绝实事求是，而且还要再控制五十年。“说三道四”是个贬义词，王治安的指向不就是广大民众吗？<br />
大邑县的一些官员告诉刘小飞，主管部门向全国所有的新闻宣传机构打过招呼：凡是有关刘文彩的文章都必须经过他们的签字同意后方可发表。也就是说，他们要封杀所有报道真相的文章。<br />
文章写到这里我不禁在想：刘文辉家族为四川的和平解放立了大功，到头来怎么落到一伙巨奸的手中，成为他们编造伪历史捞取政治本钱的牺牲品。想到这里我不禁要问：我们的党和政府郑重宣称的实事求是，依法治国和尊重人权到底是真的还是假的？改革开放已三十年了，宣传至今的刘文彩故事难道就是这个问题的答案吗？<br />
<br />
一点困惑<br />
&nbsp; &nbsp; 刘文彩庄园多年来被授予省、市、全国的爱国主义教育基地。此事本人十分困惑不解，这个教育基地是教育青少年学习刘文彩修万成堰水利工程，灌溉家乡十余万亩田地；修大邑到成都的公路，让大邑通向世界；修文彩中学，给国家培养了无数人才；修水电站，照亮家乡夜空；扶困济危，帮助贫苦乡亲共同富裕；扩建安仁镇，把一条街修成七条街建设家乡呢，还是学习主管部门那些人无法无天，强奸民意，指鹿为马，残害民众，驱赶产妇，逼死人命，编造伪历史，抹黑刘文彩，恩将仇报，杀良冒公，装神弄鬼，散布极左，把人教成野兽，给文化大革命火上加油，闹得国无宁日、民无宁日、党无宁日呢？<br />
&nbsp; &nbsp;不久前，四川大学历史系教授周久香（周是主管部门编造刘文彩故事的主要笔杆子，《刘文彩罪恶的一生》和“水牢故事”都是他的杰作，“冷妈妈”一万余字的诉苦报告就出自他的生花妙笔）又在网上著文挽救他们的谎言和骗局，周久香是亵渎自己的专业，实属史学界的败类。<br />
