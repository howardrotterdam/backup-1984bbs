													<h2>首席地主遗孙讲述真实收租院的故事</h2>
						
						
																											<div id="postmessage_52216" class="t_msgfont">刘文彩庄园里的真真假假<br />
嘲&nbsp; &nbsp;川（刘小飞）<br />
<br />
当今主管刘文彩庄园宣传的要人王治安（四川日报副刊主编）在他的一本名为《庄园秘闻》的书中说，过去“出现过个别不实之事”，现在“对虚假的东西已经校正”（见该书104页）。在此我当一回导游，带领读者到刘文彩庄园去检验王治安的“校正”。<br />
一进庄园大门，第一眼看到的是一辆福特轿车，解说词说是刘文彩为了自己的轿车能开到成都，就修了大邑到成都的公路（又不是挥霍公款）。这个谎言很不高明，没有公路之前小车怎能先在大邑呢？事实是刘文彩修公路的时间是公元一九四三年前后，一九四八年刘文彩才买了一辆二手吉普车。现展出的福特车刘文彩从来就不曾有过。在一九四九年刘文彩病重期间用的小轿车都是刘文辉借的。<br />
不过安仁过去确有一辆类似的轿车，那是一九四六年刘文彩向西康省政府要的，要来供“文彩中学”的校长和教师们使用的，刘文彩本人从未享用过。主管部门弄来这辆车其实是为了抵毁刘文彩在抗战时期为国为民修公路的业绩。<br />
小轿车的左边是“雇工院”。上世纪八十年代在一次全国文博会议上，当时的馆长吴宏远发言说：当年为了把“雇工院”布置得凄惨，就人为地改建过，降低了房屋的高度……。他的发言是留下了文字记录的。近日我亲耳听到吴宏远对采访者说：过去雇工院是修得很好的，是我们故意把它打烂的……。可见展出的“雇工院”是用来骗人的一个假货。其实“雇工院”和主人的住房是同一个档次。即使在极左年代，当年的雇工们也对主管部门的人说：他们和主人吃的饭菜是差不多的。刘小飞还听庄园的工作人员告诉他，刘文彩曾与雇工合过影。还听说雇工们空隙时常常玩牌，刘文彩有时还去观看。 <br />
在福特车的对面有一道门，门里面有一口井，解说牌标明“雇工井”，说是当年刘文彩家每天杀鸡杀鸭的屎尿血水及禽毛全部倾倒在这口井里，刘文彩就强迫雇工们喝这样的水。其实刘文彩在的时候没有这口井，雇工与主人吃的是距此十米远的同一口井里的水，刘文彩在时这是他家唯一的水井。刘文彩的庄园在解放后成了中国人民解放军西藏军区作战部队干部学校。由于住在庄园里的解放军人数众多，一口水井不够用，于1951年解放军干部学校就请了好些附近的农民来又打了一口井，这就是后来说的“雇工井”。<br />
再往前走，右边分别是“西式客厅”与“中式客厅”，这两个客厅同样是骗人的假货。“西式客厅”原本是账房先生薛副官的寝室，里面有两张普通木床，一张薛用，一张为来客备用。“中式客厅”其实是四太太梁惠玲的寝室。<br />
过了两个客厅，前面出现一对红沙雅石缸，解说词说这是刘文彩当年从宜宾公园偷运回来的。刘文彩的两个儿子对我说这红沙雅石缸根本就不是他们家的。这种红沙雅石的产地在雅安，这种雅石作品在安仁绝非稀罕之物，而是随处可见。在庄园前面的安惠里就摆放了近百件，庄园右边的商店门前也摆放了数十件，这种本地随处可见之物怎么会是宜宾的东西呢？<br />
向前走就是刘文彩的内花园，里面有两间较大的房间，主管部门当年用重金把它装修成刘文彩的逍遥宫，以此向大众宣传刘文彩是如何的奢侈荒淫。其实这两间房当年是很平常的，原本是青砖铺地，如今的木地板是主管部门装修的。刘文彩在开始一年夏天的白天在那里乘过凉，晚上不睡在那里，后来发觉那里没有风，以后就不去了，倒是他的三公子常在那里读书习字。<br />
又往里走就到了刘文彩的内院，内院祖堂的左边有一套一进二的房间，里间原是三太太凌君如从宜宾撤回来时住的地方（刘文彩偶尔从雅安回来就住在这里）。外间是刘文彩三个儿子童年时住的地方，以便大人照管。后来凌君如为了争夺财产在成都借腹生子，回来刘文彩就不准她再住在这里，而是把她赶到旁边的厢房去居住。<br />
现房间里摆了张古式的农家花床，其实这不是刘文彩家的，刘家原本都是西式床。本镇乡民李国孝一再公开申言这是他们家的床，说他们家有两张床摆在刘家庄园里。<br />
祖堂前面现摆了一座大屏风，这也不是刘文彩家的东西。<br />
祖堂右边同样是一进二的房间，解说牌上标明是刘文彩的冬季吸烟室，里间摆着一张金碧辉煌的退一步大龙床，解说词说是刘文彩当年用三万斤大米来打造的。多年来这都是地主庄园里的重头戏，以此来证明刘文彩是何等的奢侈。<br />
可是，本人听刘文彩的两个儿子说那个房间从来就不是他父亲的，而是家里为至亲准备的客房。刘文辉两次回家乡都住在这里（一次是刘湘死后，一次是来接收新公馆时）。日本飞机轰炸成都时，杨夫人来安仁躲避期间也住在这里。<br />
书归正传，我现在交待那张龙床的来历。刘文彩的家人都告诉我，说他们家从来就没有过那样的龙床。据刘小飞说，上世纪九十年代他回老家向庄园工作人员问及此床来历时，庄园前办公室主任周永轩和档案室管理员杨正等告诉他，这张床是庄园在上世纪六十年代找本地陈木匠、张木匠等人打造的，因陈木匠打造有功还安置了他一个儿子来庄园就业。又听本地老人刘结熙告诉我，那张床是上世纪六十年代经济困难时期，庄园的人找了几个本地木匠在本地雕匠廖子秋家里关起门来搞了一年多搞出来的。原来是这样!<br />
那个时代正是人民没饭吃的时候，三万斤大米不给人民吃，却用去打造一张龙床来栽在刘文彩的头上，我们的主管部门就专干这种事情。<br />
<br />
在客房的对面有一间小房间，解说牌标明是“珠宝室”。其实最初是保姆的房间，后来又是刘元贵回老家住的地方。我曾看见一些媒体报道，庄园成立初期（一九五八年），上面拨下十几万元到寄买行去购买珠宝来充实刘文彩庄园。据本人所知，一九五八年成立人民公社时，社员曾发过几个月的工资，每人七至十四元不等，全劳力每月最高工资为十四元。上面拨下的这十几万元就是当时一万个全劳力农民一个月的全部血汗钱！<br />
“珠宝室”的右边是刘元龙的房间，门的上方有一块匾，落款是黄金荣。庄园里的工作人员杨正在上世纪九十年代告诉过刘小飞，那是上世纪六十年代庄园自己制作的，漆水都不是过去年代用的那种。<br />
再往里走就是后院，那里布置有一间醒目的佛堂。听刘家的人说，他们家从没在此设过佛堂，这是他们家一日三餐吃饭的地方。<br />
佛堂的右边有一间房，当年是刘文彩家存放全家棉絮衣物类的储藏室。上世纪六十年代主管部门编造说是刘文彩的女客房，是他玩弄女色的地方，并凭空捏造出李二姐、林奶子等几个人世间从不曾有过的人物和许多荒唐下流的情节，还牵连进他未成年的三公子。为编这些无耻的性故事，主管部门还逼死一位川剧女角罗某，欠下一条命债。详情请看后文。<br />
“佛堂”对面的一个房间标明是“燕窝库”，解说员说刘文彩一家吃燕窝，一顿便吃掉多少人民的血汗。我到本地茶馆里去问那些老人，他们众口一词地说：“刘文彩根本吃不得燕窝，他是火体，吃了要流鼻血！” 现在“燕窝库”又改名“海味室”。<br />
“佛堂”旁边是王玉清的房间，这才是刘文彩住的地方。<br />
再往前走，穿过曾经称为“刑讯室”、“水牢”、“土牢”（这些事以后再写文章详细讲述）的地方便到了后花园。花园左边是一幢西式平房，进到里面就看见一组泥塑，墙角还陈列着血衣。解说词说是刘文彩杀人霸产的地方（原来这里还有两组泥塑，一组杀人霸水，一组是圈地霸产）说当年刘文彩指使狗腿子曹克明把正在田里栽秧的三个贫下中农打死在这里。<br />
据现居安仁维星街56号的曹克明之子曹登贵说：当年他父亲曹克明告诉他，当时有三个四处流窜作案的土匪在唐场抢了绸缎铺，并把老板家的人枪杀了，县里正在通缉捉拿这三个凶犯。有一天这三个凶犯跑到亲戚刘益山家中，刘益山和他母亲请了好几个裁缝用抢来的黑色绸缎为其赶制衣服。看到此事的农民就跑到乡政府去报告，乡长刘绍武便立即召集人员前去捉拿。到达后刘绍武便叫曹克明进去探虚实，曹进去后土匪就拔枪准备打他，曹为了自卫便立即拔枪射击，当场打死两人。一人逃出房外后被外面的治安队员开枪打死。<br />
刘益山是刘文彩本家的侄儿，刘文彩原对他很好，但他把家供给土匪作据点使刘文彩很不高兴（谁愿意和与土匪勾结的人做邻居呢？）于是就让他搬到街上刘文彩新修的一套房子里去住。那套房要宽大许多，又是新房，刘益山不可能吃亏。情况就是这样。<br />
解放前夕曹克明因此事被抓起来关押了两年，后查明对方确系土匪才获释，但给他带了一顶反革命份子的帽子。曹杀人属职务行为，杀土匪而戴反革命帽子非常可笑，难道土匪是革命份子吗？<br />
曹登贵还告诉我：上世纪六十年代主管部门指使当时的镇长安海山叫曹克明去谈话，强迫他承认自己是刘文彩的狗腿子，强迫他说打死的三个土匪是三个贫下中农，不然就要打他（安仁镇全是反吊起来打）。并且要他去充当“杀人霸产”故事的泥塑模特。曹克明虽是一个贫民，但他戴有反革命帽子，这种人是没有任何人权保障的，曹克明除了就范没有任何选择。<br />
泥塑完成之后，1966年5月份的一天晚上，主管部门指使镇上的干部王明轩、李银松前来抓人，把曹克明五花大绑地从家中抓走，罪名是说曹克明造谣，居然说刘文彩家没有水牢。当晚就把他送交大邑县公安局。不久县法院就以造谣罪判处曹克明徒刑十五年。从此刘文彩指使曹克明“杀人霸产”的故事就在这后花园的房子里展出了。<br />
邓小平上台后开始平反冤假错案。曹克明在牢里请人写了一封申述，不久法院以量刑过重改判五年释放了曹，那时他已服刑十四年。<br />
出来后曹克明不服，过去他与刘文彩没有任何关系，“杀人霸产”根本就不是那回事！“水牢”的故事当局也承认是假的了，那就是说他判罪的依据也不存在了。为此他到县法院去申述，县法院不敢受理。他又到地区法院去申述，地区法院也不敢受理。喊天天不应，喊地地不鸣，悲愤交加的曹克明便在县法院门口服毒自杀，以生命来作了最后的抗争！此前他一再对儿子曹登贵说：“你要为我申冤啊！”<br />
上世纪九十年代刘小飞问当时的馆长吴宏远“杀人霸产”是怎么出来的？吴宏远回答：“当年编出‘杀人霸产’的依据是：刘文彩也许知道（这件事），也许不知道（这件事）”。<br />
当时刘文彩是刘文辉系统的下野官僚，而曹克明抓土匪的治安队是县政府系统的，两者互不相干，刘文彩知道不知道都与他没有关系。何况是突发事件，事前刘文彩不可能知道。<br />
主管部门在找不到一个愿意出来充当“证人”的情况下，便说门外的一棵柑子树是当年的见证。而那棵柑子树根本作不了证。因为那是一棵沙田柚，是刘文彩后来从外地购回的一批树苗中的一棵，是刘文彩在这件事后种下的，这件事之前本地没有这种品种。<br />
在王治安的《庄园秘闻》中他照搬了“杀人霸产”的故事，但做贼心虚，没敢说出曹克明来。<br />
导游到此，我请求我们的党政部门不要再让主管部门把曹克明的塑像放在“杀人霸产”的现场，让冤魂得到安息，并给曹克明家人一个公道。抹黑刘文彩何必殃及无关的底层弱者嘛！<br />
再往里走又进了一个房间，这里放有一些斗和一架飞轮风谷机。解说员在此详细地讲述刘文彩当年是如何用大斗换小斗来剥削农民，又用飞轮风谷机来拼命吹走农民用血汗种出的谷子。因此这些斗和风谷机曾名震全国。<br />
其实这些都是假的，大斗换小斗的故事早在上世纪八十年代就被成都曲艺团的郭××在舞台上公开揭露了。飞轮风谷机也不是刘文彩的。听本地人说，那是本镇乡民廖增山的，廖是个机械爱好者，飞轮风谷机是他的一个技术实验。<br />
而当今《四川日报副刊》主编王治安在最近出版的一本名为《轰天绝唱收租院》的书里是这样宣传的：“在刘文彩残酷盘剥中，更毒的招数，是在风谷机上加钢珠（飞轮），大斗大秤，一斗租谷，只能算是六七升。请听用泪水书写出的一首歌吧：风谷机，铁滚滚（加钢珠）。地主用它来收租，五石七斗干黄谷，风来只有三斗六。年年汗水空长流，一家老小饿断肠，即使年年大丰收，也受不住这般剥削苦。……”<br />
从这个风谷机的故事就可以看出主管部门向民众宣传的刘文彩故事是何等的荒唐可笑。<br />
再往里走就到了《收租院》。《收租院》的知名度不用我来赘述。其思想性和艺术性曾被中宣部的周扬捧为“建国以来两大雕塑成就之首（另一个是《人民英雄纪念碑》）。<br />
<br />
在此我讲一件有趣的事情。<br />
一九九八年三月份，刘小飞从县城赶中巴车到安仁去。上车后司机问他：“你是去地主庄园吗？”刘小飞回答：“我回自己的老家！”司机和乘客都惊奇地问：“你姓刘吗？”回答：“我是刘家的后人，怎么不姓刘呢！”司机又问：“你是老的那边还是新的那边的？”本地人称刘文彩的家为老公馆，称刘文辉的家为新公馆。当司机听刘小飞说是老公馆的后人时立即激动地说：“我爷爷今年九十五岁了（老人家而今该是一百零五岁了），现在还在。他说的，当年在你们家交公粮的时候（把交租说成是交公粮，耐人寻味），八个人到齐了就开饭，结果编他妈Pi个啥子《收租院》出来！”接着全车的人都说：“当年别个（指刘文彩）是全县公认的刘大善人，别个当年做了好多好事啊！结果给别个编些啥子哟！”<br />
中巴车破例把刘小飞送到庄园办公室门口。刘小飞见到吴馆长后就把这些话一字不漏地重复给他听。吴没作声，旁边的一位本地老人刘泽高立即回答：“是的，有这个事。送粮的人来要请饭在大邑县刘文彩是首创。说刘文彩是善人有民谣为证，当年的民谣是：‘我们不盼家乡出贵人，只盼家乡出善人！’贵人指的是刘湘，他贵为省主席没给家乡办过实事；善人指的是刘文彩，他回家乡修公路、修学校把一条街修成七条街……，所以大家公认他是刘善人！”此时吴宏远立即打断刘泽高的话，吴说：“现在虽然讲实事求是，但还是有纪律，该说的才说，不该说的就不能对外人说！”<br />
在刘小飞的一再追问下，最后吴宏远说：“刘湘抗日、刘文辉起义、刘文彩建设家乡，这是他们三个人一生中的亮点，但上面说了，刘文彩是否定了的人物，他在人们心目中已经定型了，就不要改变他了。现在国家的第一大事是安定，如果把实情报道出去，会助长动乱。”<br />
原来当年还有歌颂刘文彩的民谣――“我们不盼家乡出贵人，只盼家乡出善人！”<br />
从上文吴宏远回答刘小飞的那些话中证实，在刘文彩事件上，上面拒绝实事求是，准确地说是不敢实事求是。<br />
再从上文提到的那位老贫下中农的那句话：“当年在刘文彩家交公粮的时候，八个人到齐了就开饭，结果编他妈Pi个啥子《收租院》出来！”有了这句话，《收租院》所有的思想性和所有的艺术性就通通变得分文不值了！<br />
在此我引用一下刘文彩故事的幕后策划者马识途的一段文章，该文透露了人民公社时期四川农民交公粮的情况：“据社员说，那几年征购粮食征得太多，把他们的口粮也搜刮得没留下多少了，有的地方简直是翻箱倒柜，整得鸡飞狗跳。有的地方关系紧张到社员反抗，不得不派武装去镇压，有的社员对我说，连机关枪都对他们架起来了。”（见《龙门阵》1995年第三期第7页）<br />
这些场景才是《收租院》的真实写照嘛！怎么拿来栽在刘大善人的头上呢？<br />
文章还有：“我们搞‘四清’发动社员诉苦时，社员一诉就诉到自然灾害那几年他们那里饿死多少人的事，弄得我们啼笑皆非。”（同上文）<br />
文章还有：“中央就向四川多征购20亿斤粮食，向中央说了大话，就要兑现，不得不把社员的口粮也征购了一部分，这样一来，哪有不饿死人的。”（同上文）<br />
既然马识途的文章提到饿死人的事，那么那几年四川到底饿死了多少人呢？<br />
据说省档案馆记录的是810万人。但这不真实，据老干部邓自力（邓小平的兄弟）的回忆文章《坎坷人生》中有这样的记载：“老陈说：‘李井泉才是地地道道的走资派，把四川老百姓害得好苦，1960年死的人上千万，据公安厅统计的还不止这个数，不敢上报。’”（摘自《坎坷人生》265页）<br />
那些年大邑县又饿死多少人呢？<br />
