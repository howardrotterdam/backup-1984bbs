													<h2>余杰：《纪念刘和珍君》为何被逐出中学语文课本？</h2>
						
						
																											<div id="postmessage_708937" class="t_msgfont">在近期的中学教科书改革中，虽然大的趋势在向着世界文明潮流靠近，但也出现了一些局部的逆流。比如，上海的新历史教科书因为淡化了中共的“革命史”而被中止使用，鲁迅脍炙人口的名作《纪念刘和珍君》被从中学语文课本中删除。<br />
<br />
&nbsp; &nbsp;对于《纪念刘和珍君》的消失，在知识界有不同的解读。近日，杂文家鄢烈山在《重塑鲁迅》一文中，谈及广州一尊新落成的鲁迅塑像，称赞这尊国内惟一的鲁迅和许广平在一起的“夫妻塑像”，鄢文指出：“与以往横眉冷对的鲁迅形象不同，这回的塑像着力凸现了他和蔼、宽厚、坚韧、睿智的文化气质。这不仅是为了‘缅怀两位先生在广州的情缘’，也表达了时人对鲁迅的新认识或者说新塑造。用鲁迅之孙周令飞的话来表达其立意，就是‘作为战士的鲁迅已经不合时了’。”紧接着，鄢文谈及当下中学语文课本中课文的更替，鲁迅作品大幅缩减，金庸小说得以入选，鄢文为《纪念刘和珍君》被撤换而叫好：“起码将《纪念刘和珍君》换下来还是有道理的。此文背景说来话长，文章中‘我向来是不惮以最坏的恶意，来推测中国人的，然而我还不料，也不信竟会下劣凶残到这地步’这句表达激愤的话，被剥离了语境，成了一些青年人振振有词的座右铭，将之理解为对人对事心怀猜疑和敌意是一种很可怕的误解，还是暂不出现为妙吧。”<br />
<br />
&nbsp; &nbsp;我完全不同意鄢烈山的看法。我不知道他为何如此见不得《纪念刘和珍君》一文，为何不愿接受鲁迅对中国人“下劣凶残到这地步”的评估，为何支持鲁迅的孙子提出的“作为战士的鲁迅已经不合时了”的论断。无疑，“横眉冷对千夫指”和“俯首甘为孺子牛”是鲁迅生命中缺一不可的两个方面。此前人们过多地接受其怒发冲冠、“横眉冷对千夫指”的一面，而忽略其温柔谦卑、“俯首甘为孺子牛”的一面，如今凸现后者、还原一个更加真实的鲁迅，确实有必要。但是，我们不能从一个极端走向另外一个极端，试图用鲁迅“俯首甘为孺子牛”的一面，来完全地取代其“横眉冷对千夫指”的一面，那样所获得的鲁迅的形象，同样是残缺不全的。<br />
<br />
&nbsp; &nbsp;在我看来，《纪念刘和珍君》是鲁迅最好的文章之一，是鲁迅最动感情和最用心血写的文章之一，是中国现代文学的经典，是中国现代历史的缩影。这篇文章作为中学语文课本中的范文是当之无愧的。一九八九年秋天，我刚刚上高中一年级，在学习《纪念刘和珍君》一文的时候，老师一边朗读一边落泪，他是用哭腔念完这篇文章的。虽然大部分同学都不明白老师为何如此感动，我却心有戚戚焉。那时，天安门屠杀刚刚过去三个月，血迹还没有干，谎言已经开始弥漫。对“六?四”期间的言行的清查正在如火如荼地展开，人人都谨言慎行。语文老师是一位大学刚刚毕业不久的年轻人，惟有讲授这篇文章的时候，他才得以真情流露。<br />
<br />
&nbsp; &nbsp;许多年过去了，老师朗读那些段落时的情景，宛如是在昨日，历历在目，这些经典的段落也时时涌上我心头：“我只觉得所住的并非人间。四十多个青年的血，洋溢在我的周围，使我艰于呼吸视听，那里还能有什么言语？长歌当哭，是必须在痛定之后的。而此后几个所谓学者文人的阴险的论调，尤使我觉得悲哀。我已经出离愤怒了。我将深味这非人间的浓黑的悲凉；以我的最大哀痛显示于非人间，使它们快意于我的苦痛，就将这作为后死者的菲薄的祭品，奉献于逝者的灵前。”《纪念刘和珍君》过时了吗？没有过时。因为屠杀并没有远去，屠杀就在我们身边；屠杀不仅是段政府的杰作，更是邓政权的佳绩。在那些日子里，万马齐喑，指鹿为马，道路以目，我们年轻的语文老师惟有通过朗读《纪念刘和珍君》来表达对死难者的哀悼和对独裁者的愤恨：“但段政府就有令，说她们是‘暴徒’！但接着就有流言，说她们是受人利用的。惨象，已使我目不忍视了；流言，尤使我耳不忍闻。我还有什么话可说呢？我懂得衰亡民族之所以默无声息的缘由了。沉默呵，沉默呵！不在沉默中爆发，就在沉默中灭亡。”这些句子深深地打动了我少年人的心，我就像当年目睹十二月党人被沙皇绞死的赫尔岑一样，由此发誓与一切专制独裁的力量战斗到底，这篇文章奠定了我的生命和我的写作的本色。十八年过去了，我不知道有多少中学生有过与我相似的经历，但我相信《纪念刘和珍君》一直是青年一代精神启蒙的重要一课。我在北大念书的时候，曾经在校园的一个不起眼的角落里找到了与刘和珍、杨德群一起遇难的燕京大学女学生魏士毅的墓碑。在月明星稀的夜晚，我独自前去祭拜孤独的学长，并为她诵读《纪念刘和珍君》。<br />
<br />
&nbsp; &nbsp;这是一篇和着眼泪和血写成的文字。这是一篇不容轻慢、不容亵渎的文字。如今，中共的教育主管部门将《纪念刘和珍君》一文从中学语文课本中删去，表明他们害怕这篇文章，担心让老师和学生们联想到一九八九年的天安门大屠杀。这篇文章不符合中共当局炮制的“和谐社会”的谎言。杀死孩子之后不仅不认罪，而且还继续逼迫死难者家属的政府，有什么资格说“和谐”这个词语呢？作为老百姓，谁不希望和谐、平安与幸福，可是谁愿意以做奴隶为代价来换取所谓的“和谐”呢？<br />
<br />
&nbsp; &nbsp;《纪念刘和珍君》所描述的不是过去的中国，而是当下的中国。这就是鲁迅文章的力量所在，“死诸葛吓死活司马”，对一个已经不存在的旧政权的谴责，居然让一个武装到牙齿的新政权心惊胆战！是的，如果说中华民国以来最黑暗的一天是“三?一八”，那么中华人民共和国以来最黑暗的一天是“六?四”，而后者的残酷与血腥程度远远超过前者。作为当代的写作者，应当自己写出一篇篇像《纪念刘和珍君》一样严词谴责凶手的文字来，并慰藉那些死难者的灵魂和他们哀伤的家人；然而，我们没有写出来，因为怯懦，因为逃避，因为升官发财的欲望。于是，我们只能借用鲁迅先生的文章，借用这篇八十多年前的老文章，曲折地、隐讳地、战战兢兢地表达我们的心思意念，这本身便是一个巨大的退步。然而，当《纪念刘和珍君》被从课本中删除的时候，当更大的退步发生的时候，杂文家鄢烈山不仅不反对之，反倒为之叫好，因为“和谐社会”不再需要“战士”了！<br />
<br />
&nbsp; &nbsp;其实，鄢烈山早已宣称鲁迅的时代结束了，今天的中国不需要鲁迅了，需要的是“公民写作”，是“建设”而非“破坏”。是的，死去的人既然不能复生，过去的事情就过去吧，要学会向前看，谁让我们生活在一个鄢烈山形容为“中国人权状况最好的时代”呢？这个时代，想当战士的人，只配落得个堂吉诃德的下场！但是，在我心目中，鲁迅永远都是战士，是独自面对各种黑暗势力、组织及流氓小人的战士，是同时受到“敌人”和“自己人”猛烈攻击而遍体鳞伤、却“怒向刀丛觅小诗”的“异类”。一旦剥离其战士的质地，鲁迅便不再是鲁迅了。战士的身份，是鲁迅与胡适、徐志摩、梁实秋、林语堂等人最大的区别。鲁迅迁居上海之后，脱离了政府、大学等建制，完全靠写作维持生活。在此意义上，鲁迅比胡适等人更加自由和独立，这种自由和独立也更值得尊敬。鲁迅不仅与北洋政府、国民党政权战斗，不仅与帝国主义和资本家战斗，也与中共控制下的左联领导人“四条汉子”战斗，与习惯吃人血馒头的大众战斗，凡是有黑暗的地方，便有他战斗的身影！<br />
<br />
&nbsp; &nbsp;我们这个时代恰恰是最需要鲁迅的战斗精神的时代。这个时代的黑暗并不比鲁迅那个时代少。鄢烈山当然有权选择风花雪月，当然有权不当鲁迅那样的战士，当然有权否定昔日也曾经战斗过的自己，甚至也有权批评和否定鲁迅的立场，这是他的权利和言论自由；但是，我们同样有权批评鄢烈山打着“重塑鲁迅”的旗号所做的歪曲鲁迅的工作，我们同样有权呼吁更多的知识分子像鲁迅那样生活和战斗。换言之，我们理解和宽容明哲保身的选择，但我们不能认同逃兵对战士的攻击。今天的中国，并不是像王蒙所说的那样“要是有一百个鲁迅，天哪，真不知道该怎么办”；今天的中国，需要有一百个、一千个、一万个鲁迅，在人权状况极其恶劣的状况下为自己及同胞争取基本人权，在缺乏言论自由的环境中为自己及同胞争取言论自由。面对山西黑窑的奴隶童工，面对辽宁被铁水瞬间蒸发的工人，面对被流氓绑架殴打的人权律师李和平，面对奥运前夕被赶出北京的上访村的访民，难道应当见怪不怪、视若无睹吗？我相信，如果你是一个良知尚未泯灭的人，必定会像鲁迅那样愤怒并呐喊。这些发生在我们身边的事实表明，现实社会的残酷与邪恶远远超过我们的评估与想象，也是对鲁迅《纪念刘和珍君》中鄢烈山最不喜欢的那段文字的印证――“我向来是不惮以最坏的恶意，来推测中国人”。<br />
<br />
&nbsp; &nbsp;同样在繁华而溃烂的广州，鲁迅研究者、作家林贤治的感受就与鄢烈山截然不同。鄢烈山拼命维护“和谐社会”的面子，甚至写文章歌颂中共十七大的“表达权”，林贤治偏偏要揭穿那一幕幕“不和谐”的画面。在广州，有被警察打死的大学生孙志刚，有被武警射杀的太石村村民，有被冤屈下狱的记者喻华峰，也有“小康”不久的鄢烈山。林贤治以鲁迅为镜子，照出当代知识分子的“小”来，他说：“我们这些学者之类的算啥？哪个人会自始至终想到沉默的大多数，哪个人会想到社会不合理的存在，哪个人会正视专制主义的存在，哪个人敢挑战专制者！我们有谁能够真正做到？有些人说鲁迅好斗，不好。这是一个很可笑的东西，恶势力在那里，斗还是不斗？这是一个很简单的事情。”然而，鄢烈山却不接受这最简单的常识，不承认战士在这个时代的价值。不是鲁迅在变化，而是某些人自己在变化，变得世故、圆滑、冷漠、自私、“过于聪明”，正如林贤治所说：“我们身上没有一点战斗的因子，我们已经失去了感受的能力。”<br />
<br />
&nbsp; &nbsp;鲁迅从来没有掩饰过对专制权力的厌恶，从来没有想过要用某种“艺术化”的方式来隐讳地表达这种厌恶。是与非、善与恶的判断，其实是很容易作出的，不需要太多的考证和研究。毫无疑问，《纪念刘和珍君》是一篇从心底里喷涌出来的文章，鲁迅在写这篇文章的时候，从来没有考虑过是否可以发表、在什么地方发表，以及发表之后会给他带来怎样的危险。这样的文章才是千古绝唱。某些人，一提起笔来便反复斟酌如何发表、在哪里发表、能得到多少稿费、会不会给自己带来麻烦。在此心态下写作，是非正常的写作，是自我阉割式的写作。在此精神状态下写出来的文章，必定没有生命力。<br />
<br />
&nbsp; &nbsp;《纪念刘和珍君》是一篇许多人不敢也不愿阅读的文章。虽然它被从中学语文课本中撤除掉了，但它不会消失在历史深处。我会把它介绍给许许多多的年轻朋友，我相信它会打动越来越多同胞的心灵，因为我们所生活的外部环境，与鲁迅生活的时代相比，并没有本质性的变化。这篇字字泣血的文章，其重量远远超过了某些帮闲文人所写的所有帮闲之文。</div>

							
							
							
							
