																											<div id="postmessage_39562" class="t_msgfont">我来说说英法火烧圆明园是怎么回事吧！<br />
<br />
作者：三糊涂<br />
<br />
（看见有人在讨论这个问题，看见大家对这个问题感着兴趣，所以从本糊涂新完成的第二次鸦片战争书稿《又挨打了》中摘出以下几个镜头供大家欣赏，为了大家阅读方便，把注解去掉了）<br />
<br />
镜头一：英法使者：在北京体验中国特色的刑罚<br />
<br />
老巴被逮，英方当时并不怎么担心，格兰特说他相信巴夏礼的才干和能力，说不定还能见到中国钦差跟他吵架呢。额尔金更是认为，巴夏礼一个人就顶一支军队。但是法军统帅孟托班认为，发动攻击是救出这些人质的最好办法。<br />
<br />
就孟托邦是对的，其他人全错了。他们太不了解中国了。<br />
<br />
僧王逮住巴夏礼时，双方是吵架了，但是吵得很不平等。僧王训了话，说：你们赢了两次，而我们只赢了一次，你们两次进犯大沽炮台，怎么如此贪得无厌？我知道你名字，都是你煽动他们干的，现在，该我教训你了。<br />
<br />
僧王教训巴夏礼的方式就是让人摁着巴夏礼的头，在地面上连续磕响头。你们不是不磕头吗？那我叫你一次磕个够。<br />
<br />
巴夏礼说：俺是谈判人员，打着休战白旗从你们防区经过的，且得到过贵方明确承诺，给予通行安全。你们现在为什么不遵守游戏规则呢？<br />
<br />
对于他的质问，僧王报以爽朗的大笑。大笑之后，给手下示意，手下走过来，摁住巴夏礼的头，又是一顿猛磕。<br />
<br />
磕过之后，僧王要求巴夏礼立即阻止联军的前进。巴夏礼说：这我作不了主。僧王觉得很奇怪，我看事事都是你作主，你咋就作不了主呢？说瞎话。巴夏礼说：全是真话。你打死我，我也阻挡不了联军的步伐。<br />
<br />
这就是鸡与鸭的对话。<br />
<br />
正对话中，前方炮声响了，僧王命人把一干俘虏装到车上，送往北京去了。中国的车子没弹簧，所以一干俘虏很不舒服。据美国学者认定，这是美国公使华若翰先生上京换约的恶梦重演。这就是美国学者的不对了，睁着眼睛说瞎话。美国使者虽然坐了同样的车，但是手上根本没捆皮绳。不象巴夏礼等人，手上被勒了皮绳，这样子坐车上，被车颠得东倒西歪没法用手扶一下，他们哪里受过这罪啊。<br />
<br />
巴夏礼与额尔金的私人秘书洛奇被关到了刑部大牢。洛奇为了联系到同志们，在牢房里唱起了鸟语歌：《上帝拯救女王》。但是他没得到回音，出狱后，他曾写过回忆录，说自己最担心的是捆在手上的皮绳，那皮绳用水湿过，据说越勒真紧，勒几天手腕处即开始腐烂并且生蛆。有兴趣的人可自己在家做做实验，不过试一天就行了，不必等蛆生出来。<br />
<br />
巴夏礼和一帮子中国囚犯关在一起，在这里，他得到了中国政府不曾给予过的尊重与友好，那些囚犯甚至尽可能的帮助他。有意思的是，刑部给他定的罪名居然是“谋反”。看来，中国政府在刑法上都是如此多情，把外国敌人当自己臣民来对待了。审讯的时候，对他也不算太狠毒，虽然威胁说要砍他的头，但顶多是拉着他的头发或者耳朵在地上拖来拖去。纵观中国人古至今的刑罚史，这种审讯算是温情多了。<br />
<br />
其他俘虏被关在了圆明园，据特拉维斯&#8226;黑尼斯三世，弗兰克&#8226;萨奈罗这两个美国佬说，皇帝曾秘密审讯过他们。这个可能是真的，否则没法解释他们何以会被关在圆明园里。估计咸丰也没审出什么结果来。一是后来一听说八里桥中方战败了，他当即跑了；二是僧王与他们对话都是鸡与鸭了，咸丰与他们对话，那就是牛对着鸡鸭谈琴了。被关在圆明园的俘虏就惨多了，双手被捆，整日下跪，三天水米未进，手腕处被皮绳勒得生出蛆虫。据后来的幸存者回忆说：《泰晤士报》记者鲍尔比第四天死去，尸体在牢房里放置三天，后被扔到野地里，让野狗吃了；安德森中尉，手脚被勒得生出了蛆虫，他看着手上的蛆虫满身蔓延，精神错乱，大叫三天，死去；一位法国犯人，蛆虫进了他的嘴巴、耳朵、鼻子，也疯了……一个幸存者居然还在狱中数蛆来着，说，一天可繁殖1000只蛆虫……<br />
<br />
唉，好好体验一下野蛮国家的野蛮法律吧。对他们道义上同情的时候，再想想咱中国的囚犯们，心里更不是滋味了。中国人民几千年都这样过来了，你们就住了几天，就疯的疯，死的死。还是缺少锻炼吧？<br />
<br />
镜头二：法方要求烧紫禁城，英方要求烧圆明园<br />
<br />
13 日至16日，英法的强烈要求下，中方陆续把其他俘虏放还，问题是联军一看这些俘虏就冒火了，卖瓜的，总共俘走俺39人，活着回来的仅19人。有些人的尸体干脆被大卸八块，还有些人腐烂得没法瞧了，还有些人找不到尸体了。再听听活人的讲述，更是毛骨悚然。卖瓜的，这个野蛮的政府。<br />
<br />
冒火与悚然之余，英法方面召开了会议，不开不行，这事儿，百年没遇过呢。巴夏礼是中国通，他知道，对中国人来说，残酷虐待囚犯，甚至要他们命，并不是什么出格的事。但是对英法这些来自西方的夷人来讲，那就太出格了。<br />
<br />
英法都同意惩罚清方，按他们的游戏规则，也就是万国公法规定，凡使臣性命不保，它日城破，鸡犬不留。但是两人提出的惩罚方式有异。额尔金的意见是：第一，这些可怜的俘虏是在圆明园内受到虐待的，那么我们就必须将这个园子夷为平地。（格兰特将军附议此点。巴夏礼更附议些点。英国的随军牧师孟纪不愧是负责灵魂那块的，干脆认为，中国的圆明园甚至不抵一个英国士兵的性命。没办法，可恶的外国佬就是这么看重他们的个体生命。他们不知道，有些人可以死得轻于鸿毛的，以为所有人的死都重于泰山）。第二，应由中国政府出钱，在天津设立纪念碑，用满文、英文、法文刻上清政府低头认罪的碑文，落成后由清方大臣护送死者尸体前往天津参加揭幕仪式。法国公使葛罗对额尔金的意见表示反对：第一，圆明园没有设防，严格说来非交战区，烧了人家，乃无益之报复（孟托邦将军附议这一点）。第二，立碑一事不好办，清政府骄傲惯了，受不得这等侮辱，刺激他们过分了，说不定会影响我们的谈判。第三，实在想报复，咱就烧北京的皇宫吧，烧皇宫比烧一个园子，更能让清朝统治者印象深刻。<br />
<br />
额尔金不同意烧皇宫，因为进京前，答应人家留京王大臣了：人家和平开放永定门，咱进去后不扰民不烧抢。现在烧人家皇宫，不是咱不讲信用了吗？再说了，北京居民没惹咱，烧皇城，相当于烧北京，何苦跟人家的百姓过不去呢？至于天津不立碑，额尔金倒是同意了。最后他就坚持一条：烧圆明园！不烧偶咽不下胸中那一口恶气。法国拦不着人家，也就不管了。于是双方各自制订了自己的最后通牒。<br />
<br />
10月16日，英方向清方提交最后通牒：第一，拆毁圆明园；第二，赔偿被虐待致死的英方人员补恤金30万两；第三，赔款未付清之前联军驻守天津。<br />
<br />
10月17日，法公使向清政府提交最后通牒：第一，恤金20万两；第二，将虐待俘虏的清方官员严加治罪；第三，让出城内肃王府的公馆作为法国使馆；第四，给还康熙年间各省天主教堂及传教人的坟茔、田产、房屋。<br />
<br />
镜头三：额尔金：我放火，我有理！<br />
<br />
额尔金要给不讲信用的咸丰一点颜色看看－10月18日，开始焚烧圆明园。虽然法国方面坚决反对，但是额尔金认为他烧得有理，为此作出了诸多解释：<br />
<br />
“ 第一，被囚诸人，手足缚系，三日不进饮食，其受如斯野蛮之待遇，即在此地。第二，若对于中国政府所为不顾国际公法之残酷行为，不予以久远之印象，英国国民必为之不满。若现即与之媾和，订约撤兵而退，中国政府必以吾国人民为可以任意捕杀无忌，在此点上必须警醒其迷梦也。皇帝避暑行宫固已被掠，然其所蒙损失，在一月内即可恢复原状。……圆明园宫殿之为要地，人所共知。毁之所以予中国政府以打击，造成惨局者为此辈而非其国民，故此举可谓为严创中国政府，即就人道而言，亦不能厚非也。”<br />
<br />
“要想取消毁坏圆明园这事，若单要求赔款，在这种扰乱的情形中，中国政府，除了民脂民膏以外，也付不出大笔款项。其次，或是要求清政府交出那般苛待英人，和破坏和约的人们，一些可怜的属员，也许要呈献出来，作替身了。假若要求僧格林沁本人，中国政府大约不能答应，更决不能实行。寻思推绎的结果，只有毁坏圆明园，似乎是唯一的方法，而且这种责罚，仅降在清文宗本身，与人民无关。”&nbsp;&nbsp;<br />
<br />
为了让大清人民明白自己的良苦用心，额尔金放火前，专门在北京张贴中文小广告，公布了放火的时间，并向北京人民作出了解释：“任何人，无论贵贱，皆需为其愚蠢的欺诈行为受到惩戒，18日将火烧圆明园，以此作为皇帝食言这惩戒，作为违反休战协定之报复。与此无关人员皆不受此行动影响，惟清政府为其负责。”&nbsp;&nbsp;他这解释在当时纯粹是自作多情。据说，“中国人看到告示后，丝毫没有为即将发生的事情操心，而是对其蹩脚的语法大笑不已”。&nbsp;&nbsp;<br />
<br />
看额尔金的意思，火烧圆明园，乃是专跟咸丰帝一个人玩的，谁让大皇上不守信用呢？是的，我相信那时候的中国百姓，心里没任何触动。假设洪秀全能打进北京，可能也是烧；不烧也是为了自己住，反正跟普通百姓没有关系。谁爱烧谁烧。不过，额尔金当时既不明白中国人民的感情，更不明白中国政府的运作路数。咸丰她媳妇儿后来发动政变时，顺手把屎盆子扣到了肃顺等顾命大臣和载垣等谈判大臣身上：“上年海疆不靖，京师戒严，总由在事之王大臣等筹划乖方所致。载垣等复不能尽心和议，徒以诱惑英国使臣，以塞已职，以致失信于各国，淀园被扰，我皇考巡幸热河。” 小娘们意思很明白：肃顺这皇家参谋一味强硬，载垣又不好好跟人家谈，失信于各国，以致于咱园子被烧，她老公不得不去热河打猎。千错万错都是手下人的错，她老公么事没有。大清这种制度，天下罕有，所有的事，都是皇帝拿主意，但是所有的事，他又都用不着负责任。总之，额尔金这园子还是烧错了对象。<br />
<br />
镜头四：额尔金搬起脚，砸上了中国民族主义的石头<br />
<br />
1861 年4月16日，额尔金到达英国。与孟托邦将军不同的是，迎接他的是各式各样的欢迎。大家都认为火烧皇帝园子太好了，只有一个人觉得不好，那就是英国首相巴麦尊，这个70多岁却一直生机勃勃的老家伙说：为什么不连中国的皇宫一块儿烧掉呢？当然还有两个人表示不安，那就是维多利亚女王和她亲爱的丈夫阿尔伯特亲王。前者觉得额尔金这行动比太平造反还要猛，太可怕了；后者担心过分侮辱皇帝并暴露皇帝的无能会不会促使清王朝走向覆灭。同为王室，惺惺相惜，兔死狐悲，两口子估计跟咸丰家还有些共同语言吧？<br />
<br />
不过，尽管大家都高叫烧得好、烧得不够，但在皇家学院的欢迎仪式上，额尔金还是向大家解释了，火烧圆明园的原因。也许是良心有所不安吧。他说，他不认为圆明园的艺术有什么价值，不过园子里的珍宝还是有价值的，它们被埋没在圆明园那个垃圾堆里。他说，没有谁比他对于圆明园的毁灭更痛心，但是他必须这样做。他不是为了单纯的报复中国皇帝，而是为了后继使臣们的安全。<br />
<br />
总之，他烧得有理。<br />
<br />
问题是历史绝对不按照某个人的一厢情愿而发展。额尔金搬起自己的脚，砸上了别人的石头。<br />
<br />
第一，火烧园子，他自认为惩罚的是失信的中国皇帝。但是中国皇帝根本不承担此种责任，前面说了，1861年，咸丰他媳妇儿发动辛酉政变时把屎盆子全扣肃顺与载垣身上了。即使咸丰他媳妇儿不政变，咸丰自己也会把屎盆子扣办事大臣身上的。大清皇帝，连大汉皇帝玩的罪己诏之类的游戏都没继承过来。人家都兴“有限责任公司”，爱新觉罗家兴“没有责任公司”。<br />
<br />
第二，惩罚的是中国皇帝，给他记恨的却是中国后世民众。虽然当时的中国民众与皇帝无关更与圆明园无缘因此对火烧一事无动于衷甚至以嘻哈态度对之－这一点，额尔金1858年底在长江考察两个月就发现，一般民众对朝代之争的任何一方都没有多大的热情，他们对战乱的态度仅相当于他们对地震、瘟疫或任何其它天灾一样。但现在的民众不这么干啊。虽然不幸生在现在的中国，无缘做咸丰的子民，但是现在的他们有民族情绪啊。虽然那园子不烧也不见得能保证现在的他们可以随便进去游玩，但是他们素质与觉悟高了，认为圆明园乃是民族遗产的一部分，火烧圆明园恰好伤害的是他们的感情，并且由此成为一个民族的集体记忆，随着时间的推移而越来越深。这是个意味深长的情结。奴隶主挨了别人一巴掌，过后可能早忘了，但是奴隶们世世代代记着主子所受的耻辱，甚至主子的耻辱就是他们的耻辱，这是一种旷世移情。而且这种移情有些吊诡，那就是，如果这园子是他们自己人烧的，比如项羽火烧阿房宫，中国人就没有这种深刻到血液里的集体记忆与种族仇恨。这也叫选择性记忆与选择性仇恨吧。不过也有人打过比方，如果有外国侵略者烧掉了英国女王的白金汉宫，那么英国人民是什么感觉呢？ 历史不能假设，有机会碰见英国人，不妨打听他们一下。<br />
<br />
第三，据说额尔金火烧圆明园的动力，很大程度上是源于《泰晤士报》记者鲍尔比在园子里的受虐致死。当时的英国媒体听说后，恨不得吃掉中国的皇帝。为了对付恶毒的媒体，他不得不用火烧圆明园来对此作出回应。问题是随着时间的推移，原先的媒体情绪与公众情绪不复存在，而圆明园的废墟则永远存在。虽然额尔金不承认在艺术方面中国人有什么可值得英国人学习的。可是，原始人随便在墙上划拉几笔到了现今都叫世界文明遗产呢，何况那圆明园是野蛮的中国人一百五十年内由众多工匠划拉出来的大手笔呢？总之，时间越推移，大家越觉得可惜，对于额尔金的火烧行动没法表示支持了。圆明园废墟，原先是中国人的耻辱，现在，更变成是英国人的耻辱。烧园子，惩罚的是野蛮，但是园子烧过之后，废墟所能体现的，却仅是放火者的野蛮。中国野蛮，你英国也不咋样啊！套用民间家长骂欺负小孩子的大孩子的那句话：你能跟他一般见识？他还吃屎呢，你也吃屎？咸丰吃屎，我们中国人可能都不太知道，但是额尔金伯爵吃屎，全世界却都知道！</div>

							
							
							
							
