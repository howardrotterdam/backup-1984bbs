													<h2>戒严部队军人凶狠杀人原因</h2>
						
						
																											<div id="postmessage_648015" class="t_msgfont">发帖者：粗汉 (时间：2010-05-28 22:26:12)<br />
<br />
--------------------------------------------------------------------------------<br />
八阕 <a href="http://www.popyard.org" target="_blank">http://www.popyard.org</a> 【八阕】 <br />
<br />
----《1989天安门事件二十周年祭》系列之十 <br />
<br />
<br />
<br />
<br />
关于解放军戒严部队军人在六四血腥镇压事件中凶狠杀人的原因，当年在北京的学生和市民中流传着两种主要说法，一是说中共当局事先让解放军戒严部队军人服用了兴奋剂，二是说许多解放军戒严部队军人经历了云南省中越边境老山战区的轮战，早在对越作战中杀红了眼。 <br />
<br />
<br />
第一种说法有很浓的传说性质，笔者早在1990年5月撰写《天安门血腥清场内幕》一书的初稿时，就明确表示怀疑。这个说法一方面缺乏证据，另一方面与笔者的亲身经历不符。1989年6月4日凌晨4点30分至5点20分，笔者坐在纪念碑底座的最高一级台阶上，近距离目睹了冲上纪念碑底座的解放军戒严部队军人的神态，他们显示出来的不是兴奋，而是愤怒，一种对“反革命暴徒”的强烈而不可压抑的愤怒。这应该与中共当局的欺骗宣传和政治思想动员有关，不是因为服用了兴奋剂。 <br />
<br />
<br />
第二种说法有所依据。包括冲上纪念碑底座的陆军第27集团军特遣分队在内，不少解放军戒严部队军人的确参加过云南省中越边境老山战区的轮战。而这些参战军人很粗野，也很凶狠，平时就让部队难以管理，不仅经常在云南省中越边境地区打架斗殴，而且还时常在云南省省会昆明市公然闹事，连警察都不敢干涉。陆军第 67集团军在老山战区参加轮战期间，甚至发生过一名士兵端枪扫射集团军指挥机关的恶性事件，军长张志坚少将中弹受伤，军参谋长粟戎生少将及时躲入桌子底下，才逃过一劫。 <br />
<br />
<br />
从1984年至1989年，解放军七大军区奉中央军委之命，派遣部队参加老山战区轮战，包括兰州军区的陆军第47集团军，沈阳军区的陆军第16集团军、陆军第23集团军，北京军区的陆军第27集团军，南京军区的陆军第12集团军、陆军第1集团军，广州军区的陆军第41集团军、陆军第42集团军，济南军区的陆军第67集团军、陆军第26集团军、陆军第20集团军,成都军区的陆军第13集团军。此外，各大军区先后组成十三个侦察大队赴老山战区参加侦察作战。在进京执行戒严任务的解放军部队中，陆军第12集团军、陆军第20集团军、陆军第26集团军、陆军第27集团军、陆军第67集团军都参加过老山战区轮战，其他陆军集团军和空降兵第15军的侦察兵，基本上也都曾经以侦察大队的番号参加过老山战区轮战。 <br />
<br />
<br />
参加过老山战区轮战，可以说是在六四血腥镇压事件中凶狠杀人的一个原因，但不是主要的原因，毕竟在老山战区所面对的是你死我活的全副武装的敌国军人，而在北京城内所面对的则是本国的手无寸铁的平民百姓。 <br />
<br />
<br />
笔者在研读了包括中国军方在内的许多资料后发现，解放军戒严部队军人凶狠杀人的原因主要有两个，一是被激发了仇恨，二是立功心切。 <br />
<br />
<br />
先说激发仇恨这个原因。一方面，由于学生运动深得人心，北京民众普遍反对戒严措施，几乎所有进京执行戒严任务的部队都遭遇到民众的强力阻拦，军民之间或多或少发生过言语和肢体冲突，以陆军第38集团军步兵第113师为例，1989年5月20日，就在北京市丰台区六里桥与民众发生流血冲突，双方都有不少人受伤。在北京全民“截”兵的情况下，解放军戒严部队的不少军人觉得受了委屈，心里憋着一股气。后来正是步兵第113师部队率先开枪射杀民众，时间是1989 年6月3日晚上10点钟左右，地点在西长安街往西延长线上的五棵松路口。 <br />
<br />
<br />
另一方面，中共当局不许解放军戒严部队军人接触民众，避免在了解学生运动的真相以后产生同情，对所有进京部队实行封闭式的管理，严格规定官兵们不能私自走出驻地。在此期间，一边对解放军戒严部队军人进行所谓的政治思想教育，反复灌输“学生运动是一场动乱”的说辞，一边对解放军戒严部队军人进行欺骗宣传，极力宣扬所谓的“暴徒”毒打解放军戒严部队军人的恶行，激发军人的仇恨心理。为了突显所谓的“动乱”、“暴乱”，中共当局极力制造军民冲突，例如本来可以利用北京地下战备通道将参与天安门广场清场行动的部队安全地运送到人民大会堂等地，陆军第65集团军等部队就是利用地下战备通道进入了人民大会堂，但故意让部分官兵弃地下战备通道而不用，在地面赤手空拳地向人民大会堂等地徒步开进，引诱民众予以阻拦，造成军民冲突，达到激发军人对民众的仇恨心理。不得不承认，中共当局的这一招数使用得非常成功。在六四血腥镇压行动中，确实有不少解放军戒严部队军人将民众当作誓不两立的敌人对待，下手毫不留情。 <br />
<br />
<br />
例如在西长安街上，一位解放军戒严部队军官二话不说，近距离，以行刑似的方式，用手枪对准清华大学学生段昌隆的胸膛开枪，将他打死。当时段昌隆手无寸铁，没有任何暴力行为，只是希望劝解对峙的军民双方。 <br />
<br />
<br />
例如在西长安街上，解放军戒严部队官兵不放过连中三枪的中国人民大学学生吴国锋，一名士兵将刺刀捅进他的肚腹，使劲往下拉，造成长大7、8公分长的伤口，一位军官又往他的后脑勺补了一枪。 <br />
<br />
<br />
例如在天安门城楼西侧的南长街南口附近，解放军戒严部队不准民众救助中枪倒地的、年仅19岁的北京月坛中学高中学生王楠，一位老太太跪地哭求，遭军人端枪威吓；途经的救护车被强行赶走，最终导致王楠不治死亡。 <br />
<br />
<br />
再说立功心切这个原因。事实上，这才是解放军戒严部队军人凶狠杀人的最主要原因。在开枪命令下达之前，中共当局已将北京学生和市民的和平请愿行动定性为 “反革命暴乱”，将六四血腥镇压行动说成是“平息反革命暴乱”，为解放军戒严部队开枪杀人制造了依据，同时也为解放军戒严部队军人提供了立功受奖的机会。中共当局有关参战军人待遇的文件规定，：“平息反革命暴乱”属于战争性质，参与平息反革命暴乱行动等同参战，凡立功受奖、受伤致残者，均享有参战军人的优厚待遇。凡立功受奖者，国家负责优先安排工作，农村户口可以转为城镇户口。这项规定对于来自农村的士兵具有极大的吸引力，而大多数士兵又恰恰都来自于农村。以陆军第38集团军为例，在进京执行戒严任务的1万多名官兵中，就有8名官兵被中央军委授予“共和国卫士”荣誉称号，有4名官兵被北京军区授予“卫国勇士”荣誉称号，有27名官兵被授予一等功，有131名官兵被授予二等功，有1311名官兵被授予三等功，逾十分之一的军人立下战功，可见中共当局为了达到镇压目的，是如何大方地奖赏解放军戒严部队军人的。 <br />
<br />
<br />
由于上述这两个主要原因，几乎是所有的解放军戒严部队军人在接到部队开进命令后，都迫不及待地杀出封闭已久的临时驻地。当天安门广场清场命令下达之时，几乎在每一个解放军戒严部队的临时驻地都是欢声雷动，磨刀霍霍，官兵们的普遍心理是：终于等到了报仇和立功的机会。据现场目睹者说，1989年6月3日晚上，陆军第38集团军和陆军第63集团军等部队在解放军总参谋部通信兵部的机关大院举行出发前的誓师大会，全体官兵杀声震天，令人不寒而栗。对绝大多数来自于农村的解放军戒严部队士兵而言，既能报仇雪恨，快意杀人，又能立功受奖，复员后留在城里工作，这是多么美好的一件事情呀。例如北京卫戍区警卫第1师第 9连士兵刘加林，就是因为获得“共和国卫士“的荣誉称号，破例提拔为少尉排长，后来转业到公安部门工作。 <br />
