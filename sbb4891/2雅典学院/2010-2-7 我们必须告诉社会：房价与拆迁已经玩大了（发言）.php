													<h2>我们必须告诉社会：房价与拆迁已经玩大了（发言）</h2>
						
						
																											<div id="postmessage_470367" class="t_msgfont">我们必须告诉社会：房价与拆迁已经玩大了<br />
――在博客中国《国有土地上房屋征收与补偿条例》研讨会上的发言<br />
刘松萝<br />
<br />
大家好！我想讲五个问题。<br />
<br />
第一，关于《国有土地上房屋征收与补偿条例》的立法取向问题<br />
<br />
评价一部法律，除了要考察它的条文，还要考察它的立法取向。就征收或者拆迁条例来说，就是要考察它是为了体现宪法和物权法的精神，保护民权和民生呢，还是绕过法的精神，以便更容易地把房子从国民的手中拿走。旧的《条例》之所以是恶法，首先就表现在立法取向上面。<br />
<br />
新的《国有土地上房屋征收与补偿条例》在条文上有进步，但在立法取向上面仍然沿袭了旧《条例》的思路。新《条例》的主要问题在于对公共利益的规定过于宽泛，而对怎样界定公共利益却没有规定。<br />
<br />
第二，警惕突击拆迁的趋势<br />
<br />
近年来，暴力拆迁越演越烈。拆迁的范围越来越大，拆迁的理由越来越离奇，采取的手段也越来越残暴。<br />
<br />
唐福珍自焚事件震动了整个社会。血与火的代价，换来了修改旧《条例》的机会。然而就是在这新旧条例交替的时机，很多地方在突击拆迁。就是在最近，几乎每天都在发生杀人和自焚的事件。<br />
<br />
可以肯定的是，假如新的《条例》对民权与民生缺乏应有的保障，那么一定会引发新的一轮拆迁狂潮。<br />
<br />
在这里，我想提到广州正在策划的大规模拆迁行动。按照规划，拆迁会涉及60万人，并且投入巨额资金。不仅如此，还规定只要有三分之二的居民签约，就可以对其他人启动强制拆迁程序。<br />
<br />
第三，中国的房地产和拆迁已经玩得太大了，太过了<br />
<br />
中国的房地产和拆迁已经玩得太大了，太过了。目前，我们已经没有什么经济增长点，从事产业和服务业已经无利可图。要想获得暴利，只有从事房地产业，或者利用拆迁来直接掠夺。<br />
<br />
面对残酷的现实，我们一方面要争取经济发展方式的变化，另一方面要更加重视人权的保障。<br />
<br />
必须指出的是，继续热衷于投机和掠夺，灾难就不远了。<br />
<br />
第四，必须关注每一个征收或拆迁的案例<br />
<br />
前面提到了立法取向问题。为了让新的《条例》在执行中能够不出现大的差错，除了要重视《条例》本身，还要重视宪法和物权法的条文和精神。为了实现这一点，公众和媒体就必须坚持不懈，关注每一个征收或拆迁的案例。没有公众和媒体的参与，就不会有公正。<br />
<br />
对于征收或拆迁中出现的问题，媒体都应该跟踪下去。<br />
<br />
为了法律，为了正义，为了人权，为了很多人受到的困难，为了警告未来的违法者，对于暴力拆迁的策划者、组织者、实施者，不论是政府官员、企业管理者还是个人，都必须加以严惩。<br />
<br />
我在文章中说过，那些暴力拆迁的策划者一定要好好地活下去，最好活到120岁。审判，一定会到来的。<br />
<br />
我说的不是气话，正义是不能被忽视的。<br />
<br />
第五，精英的责任<br />
<br />
对于目前的局面，精英人物有着不可推卸的责任。本来，很多领导人也是熬上来的，就像我们是熬过来的一样。按道理说，他们不应该独断专行。但是，有些知识分子热衷于辫子戏，热衷讲述主子与奴才的道理，这就给贪官和酷吏以灵感――原来人间是有三六九等的！在很多正式的场合，很开明的知识分子也是一口一个老百姓。此外，还有一些知识分子喜欢研究潜规则。有人说，那些研究是为了揭露潜规则，以便我们能够战胜潜规则。但在实际操作中，潜规则告诉我们，告诉贪官和酷吏们，尽管有宪法和物权法，在拆迁中是可以另有办法的。<br />
<br />
按照那种极端和片面的市场经济，房价是必涨的，是不必干预的，这其实正合某些官员的心意，让他们心安理得地卸掉肩上的责任，心安理得地去搞土地拍卖，去制造一个又一个地王。<br />
<br />
还有一些精英人物热衷鸡毛蒜皮的琐事，比如《色&#8226;戒》，比如范美忠，比如争议可不可以吃狗肉，等等。<br />
<br />
至于茅于轼主张廉租房只能有公共厕所，任志强宣称房价会永远涨下去，平民不要考虑买房子，更是对人民的挑战，必须加以回应。<br />
<br />
应该说，很多精英人物的声望，比贪官已经强不了多少了。<br />
<br />
拆迁那么大的事情，却很少有人关注。社会的冷漠，精英的冷漠，让我想起陆杞的一则寓言：一只狼每天晚上要从笼子里面抓出一只猴子来吃。每天晚上，猴子都战战兢兢，等待可怕一刻的来临。待到狼抓走一只猴子之后，笼子里面一片欢腾。白天，猴子们若无其事地嬉戏，直到夜晚再次来临。<br />
<br />
我不赞成范美忠的有些做法，但从来没有用过范跑跑的名字。有些人一面说范跑跑，一面又说他最勇敢。现在我明白了，暴力拆迁来临的时候，贪官和酷吏最希望我们变成张跑跑，李跑跑，刘跑跑。<br />
<br />
我认为，精英人物必须承担应有的责任。说得庸俗一些，假如我们说过大话，哪怕是因为喝醉了酒，那么也应该面对接下来发生的大事说一些话。就是说，自称为精英的人物，必须为社会分担应有的责任与风险。<br />
<br />
