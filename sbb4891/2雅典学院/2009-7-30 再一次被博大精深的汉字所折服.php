													<h2>从汉字整形说到“黑社会”在坛的称呼</h2>
						
						
																											<div id="postmessage_214341" class="t_msgfont">羽戈&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;<br />
<br />
　　<br />
<br />
　　教育部打算对“琴”、“亲”、“杀”等44个汉字进行整容手术，向社会公开征求意见，却引来争议声如潮，乃至被讥为“瞎折腾”，违背了中央领导所说的“不折腾”的政治精神。<br />
<br />
　　不过，我倒觉得，对教育部此举，从形式上讲，应该表扬，起码它晓得，要倾听普罗大众的呼声；而不是像某些部门，若推出什么法规政令、提高垄断物品的价格等，就搞一个小规模听证会，收买一些民意“代表”，猫在小黑屋里，大家一起举手叫好，政令就此通过。这一回，教育部为“阳光政治”打开了一扇窗，如果它的耳朵足够敏锐，真正做到从谏如流，“瞎折腾”也许能生出光亮。<br />
<br />
　　从内容上已经没什么好说，“瞎折腾”一语中的。“琴”字左上角的“王”字最后一笔由“横”变为“提”，我实在看不出美感的增值，若不仔细看，甚至察觉不到细部的变化。“亲”、“杂”、“杀”等字底下的“竖钩”改为“竖”，最后一笔由“点”改成“捺”，我是感觉到了变化――变得呆板了，生硬了。尤其是“杀”，下面原有那么一钩，写起来手腕一提，往上一顿，霎时激起了一股清冽、凌厉的杀气，让人想起古代的吴钩，想起飘逸的李白高吟“赵客缦胡缨，吴钩霜雪明”，想起少年李贺狂歌“男儿何不带吴钩，收取关山五十州”。不妨说，整个字的气象就在于这么一钩；改成一竖，相当于一个十步杀一人的侠客不再手持吴钩，而擎起一根电线杆子。汉字的象形意蕴就这样被败坏。<br />
<br />
　　如此对汉字进行整容手术，打一比方，只有两个结局：或者将李湘整成了林志玲，一如既往言辞乏味；或者将林青霞整成了章子怡，固然与国际新浪潮接了轨，那种古典主义的高贵和华美却不幸沦为现代性的奴颜婢膝。这般整形，称“瞎折腾”都太客气，简直就是暴殄天物。我们不惮以最大的恶意揣测：教育部门这么做，不外乎是一种权力欲的呈现，暗示其权柄尚未被废置，衙门虽冷，还能折腾44个汉字，让全社会为之侧目、哗然。权力伸了一个懒腰，汉字开始遭殃。<br />
<br />
　　由这一轮汉字整形，联想到汉语的千年命运，汉语所代表的知识权力如何与各种形态的政治权力发生关系，如何被政治手术刀割的面目全非。遂记起近日一条新闻：重庆警方正在“严打”黑社会，有网媒分析，按我们的官方语言，中国――至少在大陆――没有“黑社会”，只有“黑社会性质组织”，对于此次重庆“打黑除恶”，官方发言一般称“黑社会团伙”、“黑恶团伙”、“黑恶势力”，“黑社会”的说法仍然犹抱琵琶。<br />
<br />
　　依照老百姓朴素的思维，“黑社会”与“黑社会性质组织”、“黑社会团伙”，其实是一个意思，所指为同一事物。所以，尽管官方言之凿凿，民间依旧一口一个“黑社会”，逐渐市场化的多数媒体亦随大流，“黑社会”长来“黑社会”短，“黑社会”横行没人管――“黑社会性质组织”叫起来多累，严重损害了汉语的语感。而官方并未对这种口径不一的言论予以禁绝。由此愈发可见，官方语言剔除了“黑社会”，仅仅为了标明一种姿态。<br />
<br />
　　这是什么姿态呢？就像孔乙己说“窃书不能算偷”。窃书怎么不算偷呢，孔乙己争辩道，这是“读书人的事，能算偷么”。似乎“读书人”乃是一道净化剂，可以像过滤沙砾一样的耻辱和罪恶，将贬义词变为中性词。不说“黑社会”，而说“黑社会性质组织”，一如识得“茴”字有四种写法的孔乙己要与小偷（我们则是要与那些黑社会疯狂肆虐的“资本主义国家和地区”）划清界限，严阵以待。但语言究竟有无那么大的魅力，可以改变事物的本质，可以让中国的“黑社会性质组织”泾渭分明于美国的“黑社会”？自然不可能。中美黑社会固然形态有别，性质、发展方向却大同小异：一面是公司化，一面是公权力化。<br />
<br />
　　政治姿态不是靠标榜一两个语词所能挺立的。话语上排斥“黑社会”，黑社会照样默默生根发芽，荼毒生灵。民众的眼睛则是雪亮的，他们认为政府所宣讲的“黑社会性质组织”就是黑社会，正如马铃薯就是土豆，番薯就是地瓜。在这一场话语拉锯战当中，民间压倒了官方，“黑社会”压倒了“黑社会性质组织”。由此能不能说，改造语言的决定性因素，不是公权力，而是人民的生活？<br />
<br />
　　如果认同这一论点，那么“琴”、“亲”、“杀”等汉字修订与否，则应该以社会生活的流通度、便捷度为标尺，由公众根据切身的、多元化的文化知觉和美感来判定，教育部实在不必越俎代庖。同理，是“黑社会性质组织”还是“黑社会”，我们必须学会与时俱进。<br />
