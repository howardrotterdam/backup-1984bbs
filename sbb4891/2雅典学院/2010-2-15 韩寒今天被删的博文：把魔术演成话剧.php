把魔术演成话剧 (2010-02-15 17:31:11)<br />
<br />
我看到有些刘谦的粉丝觉得刘谦的魔术只要让人觉得震撼就可以了，不用想太多，也不用知道太多，更不要想知道太多。事实上，我个人非常喜欢看刘谦。但是我不光不护主，而且我更是刨根问底栏目组的，我对我比较欣赏的对象的要求更加严格。<br />
<br />
当我慢慢知道了真相以后，我觉得刘谦带给我的是巨大的遗憾，不是震撼。一个顶级的魔术师不应该这样表演魔术。可能春晚的舞台太大，所以刘谦认为观众就是电视机前的人。但我始终认为，所谓的近景魔术，真正的观众其实应该是坐在你桌子旁边的那几个人，这些人可以有一个是托，但不能全部都是托。这号称近景魔术的小品，果汁的杯子，定制的桌子，特制的硬币，无一是道具，这已经不符合近景魔术的规则了。董卿，不洗手男和刘谦身后坐的所有人，每一个都是托，甚至连电视台的导播，摄影师都是托，他们在适当的时候切换镜头，转变机位，这是不符合职业道德的。电视台在记录魔术的时候应该做到如实记叙，而不是帮助魔术师掩盖。你和一帮托在一起用道具互动，这怎么能叫近景魔术呢。魔术师的职责就是利用你的能力和手法欺骗观众，但刘谦带了一个诈骗团伙来，演了一出话剧。如果这个也叫近景魔术，我韩寒带一桌子六个托，排练半个小时，能演的更邪乎，导播和摄像再多配合一点，春晚现场我还能把宋祖英变成宋祖德，能把董卿变成董洁。以娱乐的方式娱乐观众是正道，以愚弄的方式娱乐观众，也许不知情的观众依然觉得很好，但这不是一个电视台和魔术师应该做的事情，这样做下去，明年是不是该用更多的托，甚至用电视特技了？<br />
<br />
央视新闻联播最擅长的就是使用托来欺骗全国观众，魔术应当往技术和创新上去发展，而不是一天到晚想着如何使用托。作为央视，春晚的第一要则是政治正确，第二要则是万无一失，所以我能理解他们安排一桌子托的苦衷，如果现场不幸抽中一个像我这样的观众上台，非得要求刘谦用我兜里的硬币和自带的杯子来表演，关键时刻还大叫一声，咦，这桌子里夹层玻璃怎么在转，那就事故了。但是我坚信刘谦肯定拥有不使用道具但让我震撼的一流手法，可惜央视肯定觉得这个不够震撼电视机前的观众。在政治正确和万无一失的前提上，直播是延时的，群众是安排的，歌曲是假唱的，杂技是录播的，小品是阉割的，相声已经被摧毁了，连本来就是作假的魔术还要再作点假。于是我们中国的文艺晚会是永远不会出演播事故的。<br />
