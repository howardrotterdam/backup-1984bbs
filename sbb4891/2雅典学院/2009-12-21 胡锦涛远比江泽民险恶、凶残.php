<br />
在九常委和政治局委员当中，未必没有袁世凯、叶利钦式的人物。因为是一条船上的人，在不出事的情况下，大家当然都倾向于维持现状，一旦出事而且危及政权，肯定分歧和矛盾重重就会因为利益的冲突而陡然放大、而且不可调和：在巨大的变局面前，既然共治已经无法保全利益了，就只能破局，竞相抢占有利位置、抓取有利筹码，由于激烈的利益冲突，寡头之间的公开摊派势不可免。<br />
<br />
寡头们之间的摊牌，一定会在镇压动乱的过程中发生。寡头当中，胡锦涛无疑拥有超过其他人的职权优势，这使得变乱当头他具有行事主导权上的优势。<br />
<br />
以胡锦涛的本性和经历来看，一旦社会动乱，他会本能地选择“拉萨平暴”方式，妄图再次以血腥屠杀保专制；经济没有崩溃的时期，这么做自然容易成功，胡锦涛本人，当年不是在拉萨屠杀“平暴”成功了吗？但是，在金融崩溃的情况下，调动军队杀人局面很难控制、后果既凶险又难以预料：<br />
<br />
