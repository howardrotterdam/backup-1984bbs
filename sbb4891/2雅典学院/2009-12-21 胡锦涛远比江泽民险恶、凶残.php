全面的社会动乱会冲破寡头共治的权力平衡，迫使寡头们激烈分化、共治权力结构崩解。众寡头平时虽然抱团竭力维持现状，可一旦社会动乱不可收拾、政权岌岌可危时，就大不一样了。中共九常委之所以平时能抱团，仅因为利益需要，根本不是出自同心同德；事实上，中共九个寡头之间，各人的见识、能力、思想观念均有巨大的差别，相互之间矛盾重重、互不服气；就已知的情况来看：<br />
<br />
胡锦涛乃陈云余孽，系宋平、邓力群培养出来的硬党棍子，其思维方式停留于上世纪五十年代，顽固坚持共产党专制；江泽民及其代理人则热心于营造官僚资本党，心向李光耀、苏哈托式的独裁统治；温家宝内心倾向于民主宪政，但又反对激进市场化，其思想观念接近社会民主党。<br />
