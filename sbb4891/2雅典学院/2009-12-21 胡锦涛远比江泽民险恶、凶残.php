在下今年三月在拙文《胡锦涛，没有“新思维”的戈尔巴乔夫》一文中，提到了胡某人与戈氏的历程有神似的一面，胡某人完全有条件成为中国的戈尔巴乔夫，在下呼吁其进行政治体制改革。然而此后的事实再次无情地证明：胡锦涛是与戈氏完全相反的那种人：戈尔巴乔夫在增强个人权力的过程中表现出来的是民主化改革的逐步深入；胡锦涛在加强权力的过程中则表现出专制控制的全面加强和专制意识形态的再度回流（崇毛热、“反资产阶级自由化”）。<br />
