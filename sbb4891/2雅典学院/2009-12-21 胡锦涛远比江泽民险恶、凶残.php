<br />
胡锦涛身被八九“拉萨平暴”和零八“西藏镇暴”双重血债，又负有继续镇压法轮功六年的罪责，诚可谓罪孽滔天、恶贯满盈，为现今中共寡头中罪恶最大者，为了防止被清算，胡锦涛会死保专制负隅顽抗到底，而不可能作任何让步；<br />
