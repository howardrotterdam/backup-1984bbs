二九年一月十三日<br />
胡锦涛的真面目及其鬼域伎俩<br />
胡锦涛究竟是怎么样的人？现在谜底已经完全解开：胡锦涛是一个不折不扣的极端的专制主义者，其本质是不择手段的嗜权狂。<br />
<br />
迄今为止，胡锦涛的这一邪恶本质已经完全得到证实。铁证如山的事实是：随着胡锦涛权力的加强和巩固，他不仅没有丝毫的民主化改革的动作，其对自由、民主、人权诉求的打压反倒是越来越疯狂。<br />
<br />
2004年九月十九日，胡锦涛取得中共军委主席职位以来，下发中央文件，狂喊要向朝鲜、古巴学习，严令加强镇压和控制；紧接着，当年年底，抓捕赵岩、师涛等独立记者、绑架威胁刘晓波、余杰、王怡等独立写作人士，并非法操没其财产，违宪重判异议人士清水君；以胡为首的中共高层，掀起一波迫害良心律师的逆流，、郑恩宠、郭国汀、高智N等人，受到残酷迫害、威胁、乃至抓捕...同时，对法轮功的残酷迫害仍然继续。<br />
<br />
除迫害法轮功为继承僵贼泯的暴政，其残酷性暂时未超过僵时期外，胡锦涛的其余所有暴政专制，都超越了僵贼泯1992年―2002年的十年统治。十多年来，僵贼泯虽然也对持不同政见者、信仰组织横加打压，要“将不稳定消灭在萌芽状态中”，但其重点是组织和活动，对知名异议人士、中共党内自由派倒也不敢轻动。胡锦涛却不仅要“将不稳定消灭在萌芽状态中”，还要堵住“错误思想”的源头，“不给错误思想的传播提供载体”。早在2002年，胡锦涛就亲自下令开除刘军宁出社科院，因为刘出书倡扬自由民主；从2003年下半年起，胡某人连出狠招，禁播《走向共和》、查禁《往事并不如烟》、《中国农民调查》，去年九月以来，胡锦涛全面严打敢言的报刊、杂志、更加严密的封锁互连网、疯狂镇压上访民众；今年三月十三日，胡锦涛拿到国家军委主席一职后，变本加厉的强化专制镇压措施：百倍疯狂的迫害上防民众，五月一日又推出新的《上访条例》，进一步残酷剥夺公民权利，企图将访民阻挡在北京之外；进一步取缔报刊、压缩有责任心的学术组织“研究所”独立生存空间，以至于茅于轼惊呼：“改革与战略研究所”在奸贼泯时代生存了十年都无问题，现在快生存不下去了―连“擦边球”都不能打了。<br />
<br />
从去年年底开始，胡锦涛又开始了加强专制能力（所谓“加强执政能力”）的“保先”愚党运动，企图以制造紧张气氛来加强专制控制。今年年初开始，胡共中央又进一步加强了对法轮功、独立宗教组织的镇压；对互联网的封锁升级。<br />
<br />
最赤裸裸的暴露胡锦涛专制暴君面目的莫过于其对赵紫阳丧事的冷血处理、对悼念赵紫阳民众的残酷镇压。陈毅死时，毛泽东尚能遵从“死者为大”的传统，悼文中未加微词；周恩来死时，面对老百姓自发的大规模的悼念周的行为，毛泽东、“四人帮”尚且没有阻止；赵紫阳死后，与赵在立场、观点上不共戴天的“左王”邓力群尚且能够亲往祭悼...而胡某人不仅对死者家属不闻不问、百般阻挠丧礼、还要在悼文责骂其“犯了错误”，给冤死的长者一记耳光！根本不用说专制了，由此足以见胡锦涛天良丧尽、人性泯灭的程度！<br />
