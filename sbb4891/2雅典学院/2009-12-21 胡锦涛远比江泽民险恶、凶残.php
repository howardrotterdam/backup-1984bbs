胡锦涛早年的整个受教育的黄金岁月，都处于毛泽东时代，并且，他受到了最完整的毛泽东时代的教育，马列主义、斯大林主义、毛泽东思想对他的影响不可能不深入骨髓；从感情上说，毛泽东时代最恐怖的时期，他个人却一帆风顺：被&quot;党组治&quot;选送到最好的大学深造，在大学里是风光荣耀的政治辅导员，在文革里也没受到任何冲击，而且能够优哉优哉地免费游山玩水，毕业后仕途也是一帆风顺...这样的经历，使一个人很难对共产专制危害有深切的体会，胡锦涛在个人感情上必然深爱毛泽东时代和造就了他的共产专制制度。有些人以胡锦涛的父亲胡静之在文革中被整死，来证明胡锦涛的内心对共产专制有怨系，这是完全站不住脚的。胡锦涛走上仕途以后，四十年来从没回家乡看望他的养母刘秉霞，相比之下，江贼民虽然邪恶，访问美国的时候还知道去看望他在上海交大时的老师；二五年大纪元4月25日讯《胡锦涛祖屋闹买卖纠纷屋主索价50万》，披露：江泽民当权时代，身为中共中央常委、国家副主席的胡锦涛为了表白自己的&quot;清廉&quot;，居然批示同意对自己养母刘秉霞老屋的强制拆迁，以致于当时已八十多岁刘秉霞老人为了捍卫自己的权益，不得不以老弱之躯挡在铲车面前......可见胡锦涛的人性泯灭到了什么程度!这样一个只有党性，人性泯灭的家伙，怎么会在乎自己的父亲怎么样呢？更何况，胡锦涛就没有从小跟父亲一起生活，感情本来就淡泊；更何况，胡静之被整根本不是因为政治原因，而有贪污之嫌...胡锦涛这么个灵魂深处都饱浸共产毒素的人怎么可能有&quot;退出中共，另立新党&quot;的觉悟呢？<br />
<br />
从罪行上说，胡锦涛是难以救药的，因为胡锦涛的罪恶比江贼民更大：所有江贼民犯下的罪行（包括迫害法轮功），胡锦涛都犯过，而且有过之而无不及；而胡锦涛犯下的一些严重罪行，江贼民却没犯过，如：对藏民大屠杀；下令屠杀广东汕尾维权农民。如果正义得到伸张，胡锦涛必将和江贼民一样被押上审判台。只有死保现存的制度，他才能避免被追究。<br />
<br />
从既得利益上说，胡锦涛现在集党、政、军三权于一身，共产党专制制度，给了他如帝王般的权力、巨大的荣耀和家族暴富的机会，而共产党专制制度一旦削弱，他的地位和既得利益就会立即受到挑战；专制制度如果瓦解，他以前屠杀藏人等罪行就会被追究，胡锦涛也没有另立新党的威信、觉悟、知识和能力...因此，从切身利益上说，胡锦涛绝无可能&quot;退出中共，另立新党&quot;，他只会进一步强化专制制度，他连嘴上说说的&quot;党内民主&quot;都不会真正实行。<br />
<br />
以上可见，胡锦涛是一个根本不值得呼唤的人，对胡锦涛的呼唤连对牛弹琴都不如。<br />
<br />
可惜许多聪明的人士对此全然不查，焦国标教授、孙文广教授等人今天还对胡温政改乐观地满心期待；前中共新华社记者吴宝璋于今年元旦写了一篇虚拟新闻《中共终场前的最后一天》，纯粹是娱乐和玩笑，竟引得唐子等人激动莫名地揣测：&quot;何许仁&quot;是不是指胡锦涛？唐子等人还言之确确地预测：二七年秋冬之际，胡锦涛必然在各种压力下逮捕江泽民，然后解体中共云云。<br />
<br />
且不说胡锦涛有无逮捕江泽民的意愿和能力，以胡锦涛的本质来看，即使他逮捕了江泽民，他也断无可能给法轮功平反，因为《九评》出台之后，法轮功已经彻底地否定了共产党。相反，一旦胡锦涛清除了所有政敌，他必定向着北朝鲜的方向，肆无忌惮地大步迈开步伐，届时中共政权对人权的迫害、对法轮功的镇压，将比起现在有百倍过之而无不及！<br />
<br />
对胡锦涛的呼唤，可以休矣。建议所有还在呼唤胡锦涛的海内外民运异议信仰人士，省下呼唤和幻想的精力来，用于大力揭露胡锦涛的邪恶嘴脸，以加速中共的解体，是时候了！<br />
<br />
