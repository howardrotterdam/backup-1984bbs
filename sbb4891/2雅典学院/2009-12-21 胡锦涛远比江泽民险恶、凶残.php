中共垮台路线图<br />
2009年对中共来说，是一个灾变的大凶之年，随着经济的全面衰退，中共政权赖以维持统治的所有条件正在急骤地丧失，这些条件包括：知识分子的“安分”、中产阶级的拥戴、中共地方官僚的效忠等等，其中，最重要的是：在经济、社会危机的双重冲击下，维系中共政权二十年稳定的寡头共治的权力结构今年必将土崩瓦解。2009年注定成为中共政权由衰落转向灭亡的元年。<br />
<br />
内部权力结构的崩解为什么是中共政权灭亡的最重要因素？这是由共产政权的性质决定的。王力雄先生指出：专制亡于内，这种灭亡方式，又以共产政权最为典型，共产政权封闭性和内向性，决定了它具有很强的镇压能量，历史上没有哪一个共产政权是纯粹被本国民众推翻的，前苏联和东欧的共产政权，无一例外地亡于统治集团内部的分裂（或游行示威等外因诱发的内部分裂）；共产政权之所以注定在数十年内垮台，并非因为其专制和残暴，而是因为其性质的不稳定性，这种不稳定性决定了共产政权必因内讧而瓦解。<br />
<br />
经历“文革”、“六四”，并且亲眼目睹了“苏东波”的中共统治集团显然看到了这一点，由邓小平、陈云带头，江泽民、李鹏完善，磨制出一种新的专制统治方式――寡头共治，这种方式有别于一切共产国家传统的专制统治方式――领袖独裁。<br />
<br />
中共的专制统治方式早从华国锋下台起，就开始走向寡头共治。华国锋下台后，中共政权形成邓小平、陈云两大元老共治的局面。邓小平虽居主导，却受到陈云、李先念等人掣肘，远不能一言九鼎；<br />
<br />
江泽民时期是江泽民和政治局七常委共治的局面。江泽民虽然号称“核心”，只不过在七常委中稍占些优势而已，却无法自行其是；<br />
<br />
胡锦涛更是连自命“核心”的权威都没有了；胡时代的权力格局，是最典型的寡头共治局面，如僵尸一般僵硬颟顸的胡锦涛，连江泽民那点个人“做戏”魅力都没有，只在侵犯人权等维护党天下利益的行为上放得开手脚，他封网禁书固然肆无忌惮，但在其余问题上得看众常委的脸色行事，中共党魁一职，在胡锦涛身上，不再具有说一不二权威，俨然成了中共寡头集团的主持人和协调者的职位。<br />
<br />
在的中共，就象一条九头恶龙，其庞大专制躯体由中共中央政治局九个魔头共同驱动，中共党魁不再能一个人说了算，重大事情必须按九魔头中的多数意志行事。<br />
<br />
寡头共治，尤其是邓小平、陈云死后的多寡头共治，是一种谁都不负责任的一种专制统治方式，但也是一种非常稳定的专制统治方式，它能够把内斗的激烈程度和破坏力降至最低。<br />
<br />
邓小平、陈云的共治局面，使得政治观点的分歧不再危及身家性命，毛时代你死我活的宫廷斗争也就很快消失；邓小平、陈云死后，“垂帘听政”结束，皇帝和“太后”的内耗隐患消失，“路线斗争”时代彻底结束；江泽民退位后，贪权恋栈的内耗隐患消失，中共形成七十岁退休、最高职务连任不超过两届的“规矩”；胡锦涛时期，实权分散在众常委的手中，中共在十七大上形成了“接班人”人选由常委多数议定的规矩，以及总书记＋总理“双接班人”的规矩，这就形成了最高权力交接班的制度，最大程度地消减了最高权力交接内耗，这一共产国家的最大内耗；以及总书记＋总理“双接班人”的规矩的确立，又很大程度上平衡了派系之间的利益分配。<br />
<br />
因为寡头共治改变了共产政权内部斗争的你死我活性质，而且，随着寡头共治的完善和成熟，政治斗争的残酷程度和激烈程度都不断下降，因此，在政权能够照常维持的情况下，各个寡头之间哪怕分歧再大、矛盾再深，也会竭力地共同维护一党专制，因为一党专制是他们共同的命根子：有一党专制在，怎么内斗都伤皮不伤骨、照样安享特权；中共一旦垮台，这些人都成了落水狗、甚至面临灭顶之灾。可见，在正常情况下，政治局那九个尔虞我诈、貌合神离的寡头怎么会不死心塌地地共同维护中共政权？<br />
<br />
因此，寡头共治是一种无须政治强人就能够自我稳定的专制统治方式：出乎许多人的预料，邓小平死后，中共政权不仅没有陷入混乱，而且更加稳定地维持了十一年，这十一年当中，中共高层的内斗比任何时候都小、权力运作比任何时候都“程序化”。<br />
<br />
胡锦涛时期的权力结构，是一种更加稳定的多寡头结构，它是成熟和精致的寡头共治。胡锦涛时期，通过更加纯粹的逆向淘汰筛选上来的九个寡头们，几乎清一色地不是屠夫酷吏，就是超级贪腐分子，为了维护巨大的既得利益和防止被清算，这些精细谨慎的技术官僚们，不仅与各自的派系拥趸紧密抱团，各寡头派系也达成了“做事不做绝”的默契――即相互之间的争斗不影响专制稳定、决不逾越一党专制底线，以维护他们共同的最大利益。<br />
<br />
中共统治寡头共治化的一个结果就是：共产政权常见的“路线斗争”等激烈内斗消失、甚至“权斗反腐”也大大减弱，统治集团高层的稳定性和自我维护能力大大增强，这一结果，让那些坐等中共内斗“解体”（如期待“胡锦涛抓捕江泽民”）的人无比失望。<br />
<br />
不能不承认，寡头共治很大程度上消减了共产政权的内耗，中共政权在“六四”大屠杀之后得以稳定近二十年，主要靠的就是一种寡头共治的权力结构。<br />
<br />
那么，寡头共治能否让中共摆脱共产政权“其兴也勃，其亡也忽”的宿命呢？答案是否定的，因为维持寡头共治局面是需要条件的，条件就是社会的稳定。八十年代初，中共靠包产到户、发展乡镇企业和个体经济等松绑措施，拯救了濒临崩溃的经济，换取了社会稳定；一九八九年，中共以一场血腥的大屠杀阻断了中国的自由化转型，以恐怖和高压换取了社会的稳定；一九九二年邓小平“南巡”之后，中共大力笼络知识分子、精英阶层、官僚队伍：<br />
<br />
一九九二后，江泽民、李鹏开始给教师、公务员队伍和事业单位员工加工资，此后一加再加，干部知识分子待遇大为提高，“脑体倒挂”消失；朱F基时期中共大力笼络官僚队伍，朱F基推行扩大内需的“积极财政”政策，国家大力投资基建、城市改造，为各级官僚“先富起来”另辟捷径；胡锦涛上台后大力加强土地出让的政府垄断，使得“卖地”完全成为官僚暴富的“新干线”，从此掀起了房价疯涨潮、延续至今的各地政府强迫拆迁、强迫征地的狂潮……<br />
<br />
官僚队伍、知识分子和中间阶层是支撑一个政权的三根支柱，中共之所以能在“六四”之后维持了十多年的社会稳定，靠的就是以上的笼络措施。<br />
<br />
但是，笼络措施的维持也是要有条件的，条件就是经济的持续快速增长，只是在任何一个国家，经济都不可能长久快速增长的，如今，随着经济大衰退的全面来临，这条路已经走到尽头：<br />
<br />
江泽民、朱F基虽然创造了经济繁荣，但那种粗放型发展和重外资、轻民企的快速发展是难以长久的，胡、温上台后，对经济采取了加强集权和行政干预的错误做法，加速了经济危机的到来；正当胡、温经过六年努力把经济搞垮之际，西方世界也陷入一场规模巨大的经济危机当中，进口锐减、外资收缩、能源和原材料价格高涨，这对于严重依赖外资、依赖进口能源、依赖出口的中共国摇摇欲坠的经济，不啻是狠狠踹上一脚。随着出口锐减、制造业倒闭潮的持续扩大，中共税收剧减成必然，没有收成，拿什么去笼络呢？在衰退潮的冲击下，苦撑已久的房地产崩溃在所难免；而房地产一旦崩溃，银行的巨额坏账就会爆炸，金融必然崩溃；金融崩溃必然引发社会整体动乱，造成政权危机。<br />
