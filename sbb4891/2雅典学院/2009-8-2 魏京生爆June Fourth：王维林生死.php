													<h2>魏京生爆June Fourth：王维林生死</h2>
						
						
																											<div id="postmessage_187702" class="t_msgfont">被称为“中国的曼德拉”并获多次诺贝尔和平奖提名的魏京生先生出席了6月6日在伦敦的西敏斯大学和6月7日在剑桥大学召开的《勿忘June Fourth：历史，现实与未来》讨论会，与中西人士一起缅怀“June Fourth”，回顾历史，讨论中国目前的热点话题，分析中国民主运动未来的走向。以下报导是本报记者根据两个讨论会的记录综合整理做出的。<br />
<br />
回顾“June Fourth” <br />
<br />
参加讨论会的人们有的是青年学生，对于“June Fourth”到底发生了什么事情一无所知。还有人并不相信中共的军队杀了人。魏先生说，“到底‘June Fourth’有多少人死了？1993年北京卫生医疗机构统计是死了6700多学生，不包括中央国家机关的，也不包括军队在广场上直接处理的。后来（军队）调来了直升飞机，开始是说运弹药，实际是运尸体。有空军人士说，直升机运了4天尸体，北京实际上死了有上万人。” <br />
<br />
魏先生表示很多的官兵们当时并不想杀自己的同胞。38军的徐军长公开抗命，结果被抓起来了。但是杀人的命令是从上面下达的。他特别提到了他所知道的一名勇士只身挡坦克的情况。他说，“其实木樨地的王维林，（人们）根本不知道他叫什么名字。这事是我的老同学处理的。当时，38军从复兴门这条路开过来，最前面的那个师的师长就是我的老同学。他就坐在首车里，所谓的王维林在那儿挡的是他的车。国外看到的镜头和国内看到的不是一个镜头。国内中央电视台放的是复兴门的镜头，国外是北京饭店的。两个完全一样的人，做同样的事，镜头略微有点差别。当时，驾驶员问他（我的同学）怎么办，能不能绕过去。所以，你能看到（录像上）坦克绕了一下。可小伙子横着过来了。驾驶员又问，师长怎么办？ <br />
<br />
没过10分钟，天上的直升飞机来电话了，问：你们怎么回事？他们说，前面有老百姓挡着。直升飞机上的人说，‘什么老百姓，是暴徒！’他说，‘我看了确实是老百姓，不能向前走。没有什么暴徒。又过了5分种，直升飞机可能请示了上级。飞机上的人说，‘首长命令了，你们马上进到军事博物管院里调整，让113师上。’113师的师长刚刚升了官。我的哥儿们（记者注：哥儿们是北京方言，即朋友的意思）就告诉侦察连的把这小子抓起来。至少先别让他死了，这人的生命太危险了。可是不知道怎么搞的一个排的侦察兵居然没有看住他，让他给溜出来了。他又去挡的时候，113师的坦克连停都不停，哗一下就过去了，直接就把他捻成肉饼了。而且那时侯，人身上什么都不带，你也不知道这人是谁，根本不知道这人是谁。” <br />
<br />
魏京生谈到“June Fourth”大屠杀的背景时说，“邓小平在“June Fourth”中杀人，为了树立他的权威。Zhao Ziyang希望通过这场运动使邓小平下台。但是邓小平代表的是一些利益集团，他们是不允许你们学生这么干的，所以邓小平最后决定杀人。当时在广场上都是支持学生的。学生们提出了反腐败。当学生又回到天安门时，我所在的监狱里的警察就说可能中国共产党要开枪。所有人都希望解决这个问题。如果不杀人，共产党要对整个社会让步，解决了对谁都好，对中共、对老百姓都好。当时在广场除了中共中央委员会的牌子没有，连中共中央办公厅的牌子都有，国务院的大旗都有人看到过。干部都是趋向学生这边的。很多人都是反对镇压的。譬如，洪学智（军委的执行副主席，有实权，可以指挥军队）秦基伟（国防部长）都是直接抵制的，民心所向吗。 <br />
<br />
他还评论说，‘June Fourth’仿佛煽了所有人一个耳光，大家的希望破灭了，对共产党的希望破灭了。对整个社会的希望破灭了，对学生民主运动的失望，对所有的东西都很冷淡。对共产党的失望。没有对它的信仰，打破了几十年来共产党对中国人教育，人们对共产党的迷信。破除了这个迷信。” <br />
<br />
70年代的西单民主墙 <br />
<br />
应与会者大要求，向大家介绍了70年代末的西单民主墙，他所倡导的“第五个现代化”，以及为此他在中共的监狱里无辜地被关了18年的一些细节。他说，“当时民主墙的大部人是为中共荐言的。在民主墙那里卖杂志很快就被人抢购一空，10元一份。民主墙的气氛很热烈。当时很多人不同意我们的观点。他们说，你们说共产党要倒台？因为从小受的教育，觉得共产党是爱人民的。当时我们说的“第五个现代化”很多人觉得很好，也有很多人不能接受，人们觉得邓小平改革了，干吗还要政治民主，还要政治现代化呀。有人也提出这个看法：能让我们过好日子就行了，以后再说吧。79年大家情绪很热烈。当时邓小平玩了一个手腕：老百姓需要经济变好，先把经济搞好了，人民就会支持他。他先打了一仗，打越南，转移视线，建立自己的威信。越战中中国的死亡人数很大。而且打完仗以后，边界线还往后退几十米。那个战争是有背后的政治目的的，普通的老百姓很难知道的。</div>

							
							
							
							
