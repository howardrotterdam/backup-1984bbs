6月6日在伦敦的西敏斯大学和6月7日在剑桥大学召开的《勿忘June Fourth：历史，现实与未来》讨论会上，魏京生作为主讲，与参加讨论会的人们一起回顾历史，讨论中国目前的热点话题，分析中国民主运动未来的走向，尤其是回答那些对June Fourth一无所知的青年学生的置疑。<br />
<br />
魏先生说，「到底June Fourth有多少人死了？1993年北京卫生医疗机构统计是死了6700多学生，不包括中央国家机关的，也不包括军队在广场上直接处理的。后来（军队）调来了直升飞机，开始是说运弹药，实际是运尸体。有空军人士说，直升机运了4天尸体，北京实际上死了有上万人。<br />
<br />
大纪元记者唐英，蒋馨根据魏京生在两个讨论会的记录整理出一份报道来。其中解开了20年前孤身挡坦克的英雄王维林的生死悬念。<br />
<br />
报道说，魏先生表示，很多的官兵们当时并不想杀自己的同胞。38军的徐军长公开抗命，结果被抓起来了。但是杀人的命令是从上面下达的。<br />
<br />
他特别提到了他所知道的一名只身挡坦克的勇士的情况。他说，「其实木樨地的王维林，（人们）根本不知道他叫什么名字」。原来王维林并不是这位英雄的名字，不过那是不是他的真实名字已经不重要了，「王维林」这三个字已经见证了中共暴政的一段历史。<br />
<br />
魏京生说：这事是我的老同学处理的。当时，38军从复兴门这条路开过来，最前面的那个师的师长就是我的老同学。他就坐在首辆坦克车里，「王维林」在那儿挡的就是他的车。<br />
<br />
当时，驾驶员问魏京生的老同学怎么办，能不能绕过去。国内录像上可以看到，坦克绕了一下。那小伙子又跟着过去，横在坦克车前面。驾驶员着急的问：「师长怎么办？」整个坦克部队停下来了。<br />
<br />
没过10分钟，盘旋在上空的直升飞机来电话了，问：「你们怎么回事？为何停止不前！」他们说，前面有老百姓挡着。直升飞机上的人说，「什么老百姓，是暴徒！」那位师长说，「我看了确实是老百姓，不能向前走。没有什么暴徒」。<br />
<br />
又过了5分钟，直升飞机里的人请示上级完毕。下令说：「首长命令了，你们马上进到军事博物馆院子里调整，让113师上！」──113师的师长刚刚升了官。<br />
<br />
魏京生说，老同学知道这个挡他坦克的年轻人生命处于危险之中，于是告诉侦察连把这小子抓起来。实际就是把他保护起来，别让他被压死。于是「王维林」被带到军事博物馆。<br />
<br />
不知道怎么搞的，一个排的侦察兵居然没有看住他，让他趁乱溜出来了。他又去挡的时候，「113师的坦克连停都不停，哗一下就过去了，直接就把他碾成肉饼了」。<br />
<br />
魏京生说，国外看到的摄影镜头和国内观众看到的不是一个镜头。国内殃视放的是复兴门坦克绕行的镜头，国外放的是北京饭店附近「王维林」和113师坦克对峙的瞬间。两个地点同一个人，做了同样的事，知道内情的人才会发现差别在哪里。<br />
<br />
魏京生说，那时侯，人身上什么证件都不带，你也不知道这人是谁，根本不知道这人是谁。<br />
<br />
还是让我们叫他「王维林」吧。叫惯了，更何况这个名字已经成为一个象征。<br />
<br />
今天是中共的「八一」建军节，这天知道了王维林的下落，还知道了June Fourth北京最少死了上万人，这决不是偶然的。△<br />
