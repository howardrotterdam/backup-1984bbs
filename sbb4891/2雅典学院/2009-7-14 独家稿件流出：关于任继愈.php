													<h2>独家稿件流出：关于任继愈</h2>
						
						
																											<div id="postmessage_161254" class="t_msgfont">《国家历史》稿件，8月号，各位朋友10日内请勿转载。<br />
<br />
<br />
一言难尽任继愈<br />
<br />
宋石男<br />
<br />
前国家图书馆馆长、哲学史家、宗教史家任继愈于2009年7月11日凌晨辞世，同日离去的还有北大教授、东方学家季羡林。比较奇特的是，前者虽然官方地位高于后者，并有国家主席到场悼念（后者则有国家总理到场悼念），但各媒体纪念文章的数量、质量，却远低于后者。当然，身后哀荣未必可以形出逝者的生前成就，但至少能折射出一波饶有兴味的社会心理。<br />
<br />
1916年，任继愈出生在山东平原县，父亲是国民党少将。“继愈”，是希望他继承“文起八代之衰”的一代文宗韩愈。不过任继愈没有按照老爹的意思，不攻文学，却爱哲学。18岁时，他考上北京大学哲学系，研究西方哲学。23岁考取西南联大硕士研究生，师从汤用彤、贺麟，攻读中国哲学史和佛教史。其中，汤用彤的影响对他最大。<br />
<br />
按照贺麟的说法，汤用彤的治学途径是以“西洋人治哲学史的方法”，参以“乾嘉诸老的考证方法”。而任继愈最早的治学思路，也大约如此。他晚年埋首故纸，主编各种巨型文献丛书，也可看作是对早年治学的一种变相回归。<br />
<br />
当说，青年时代的任继愈，跟马列主义无甚纠葛，跟同以中国哲学史名家的张岱年、侯外庐二位不太一样。任继愈以马列主义治哲学史及宗教史，是在半路；而张岱年、侯外庐则始自早年，张氏或受其兄张申府之影响，侯氏则是受李大钊影响，并曾与王思华合伙翻译《资本论》第一卷。<br />
<br />
任继愈承认正式接受马列主义，是在1956年。在任写的《熊十力先生的为人与治学》一文中，他说：“熊先生为了他的理想，生死以之。他很早就宣布他不能接受马列主义，不能相信唯物论。象他这样一位爱国的知识分子，这是可以理解的……我过去一直是儒家的信奉者。新旧中国相比较，逐渐对儒家的格致诚正之学，修齐治平之道，发生了怀疑。对马列主义的认识，逐渐明确。在1956年，我与熊先生写信说明，我已放弃儒学，相信马列主义学说是真理，‘所信虽有不同，师生之谊长在’，‘今后我将一如既往，愿为老师尽力’”。<br />
<br />
熊十力给这位昔日的学生回了一封信，说他“诚信不欺，有古人风”。然而，在这以后，据任继愈自述，他们书信往来依旧，但再不探讨学问。<br />
<br />
1959年，任继愈迎来学术生涯中最大的幸运抑或不幸――祖龙的临幸。当年10月13日，毛泽东忽把任招去，先称读过其所有著作，接着说：“我们过去都是搞无神论，搞革命的，没有顾得上宗教这个问题，宗教问题很重要，要开展研究。”随即又问他，北大有没有人研究宗教？任说除他搞佛教研究外，无人研究。毛又问，道教与福音书呢？任答，也没有人专门研究。毛最后问，你们哲学系多少人？任答，师生加起来有500人。毛说，500人一个系怎么能没有人研究宗教呢？后来，毛据此评价任的宗教研究是“凤毛麟角”。<br />
<br />
1963年，任继愈将自己1955-1962年发表的佛教论文，结为《汉唐佛教思想论集》，这是他最为人熟知的两部书之一，另一部则是其主编的四卷本《中国哲学史》。1964年，他被毛钦点，组建中国社会科学院世界宗教研究所。他在那里一直干到1987年（中间也曾下放到河南信阳干校），才调至国家图书馆任馆长，于2005年卸任。<br />
<br />
纵览任继愈的一生学术，大约有三个最重要方向：1、佛教研究2、中国哲学史研究3、主持古籍文献整理。在前两个领域，他最主要的论著，都是以马列主义学说，特别是历史唯物主义与辩证法来指导贯穿的。<br />
 <br />
在20世纪下半叶，接受马列主义学说的佛教研究者主要有范文澜、侯外庐、任继愈、方立天、郭朋、严北溟等人。其中范文澜的态度最激烈，他说，各宗教中说谎话技术最高、胆最大的无过于佛教！又说，佛经里谎话连篇，任何一部佛经决不可用认真的态度对待它，只能当作一种戏论加以唾弃。范的高论，今日听之，真要令佛学研究者肝胆耸动，汗出如浆。<br />
<br />
任继愈没有范文澜激烈，但也同样喜欢分析佛教思想的阶级属性――马列主义既然是最安全的，那就不妨尽情套；辩证法既然有时能够变戏法，那就不妨多辩/变两下。如果以这种思路来看，任继愈受命创办杂志《科学与无神论》就不难理解了，批判地研究佛教与弘扬科学与无神论，此间正有一种颠扑不破的变戏法关系。但是，1978年在南京召开的中国无神论学会成立大会上，任继愈提出的“儒教是宗教”，则至今不能令笔者理解。“子不语怪力乱神”，儒家只有现世关怀，而无彼岸寄托，怎么会是宗教？除非修改宗教的惯常定义。就在任提出此说后不久，世界宗教研究所专门成立了儒教研究室。若以小人之心度之，大可将此说看作是抢地盘的一篇露布。<br />
<br />
说回来，对于佛教及其思想，任继愈仍怀有一定的温情与推许，他承认佛家逻辑与论证方法相当精致，于是委婉地提出，可将佛教当成哲学史上的一个“反面教员”，通过对佛教唯心主义的批判，促进中国唯物主义的深化。这中间有没有一些曲线救佛的意思呢？哲人已没，无法回答。<br />
<br />
再看中国哲学史。1949年后，大陆的中国哲学史研究也是一门显学，在1980年代以前，最主流的当是任继愈、冯友兰、侯外庐，稍小一号的还可加个杨荣国进来，1980年代后，则要放入一位张岱年（此前，在反右及文革中，他均受迫害）。<br />
<br />
这几位中，冯友兰特别值得一说。他在民国即因一部《中国哲学史》得享大名，1949年后与时俱进，却常遭遇尴尬。1957年，他给毛写信，说愿意进行思想改造，要用马列主义重写中国哲学史。毛回信说：“你愿意接受改造固然很好，还是以老实为宜”。直到“文革”后，冯仍坚持以“新方法”写哲学史，晚年成《中国哲学史新编》七卷，工具之一仍是唯物史观。<br />
<br />
与冯在踉跄中坚持马列主义不一样，任继愈的中国哲学史研究显得更加稳健，也更为当轴认可。他主编的四卷本《中国哲学史》（冯友兰也有参与），1962―1974年出齐，主要手段就是以新式武器马列主义攻占所有旧的哲学思想堡垒。该书从问世起，占领中国各大学哲学系几达30年。但在今日，爱读这部书的人却不多了。人们似乎更愿意去读出生在这部书之前的，如胡适的《中国哲学史大纲》、冯友兰的《中国哲学史》，或者出生在这部书之后的，如劳思光的《新编中国哲学史》。而张岱年在《近百年来的中国哲学史研究》一文中，甚至对任继愈主编的这部书只字不提。（张岱年本人其实跟任继愈颇有渊源。八卦是这样的：张岱年是冯友兰的堂妹婿，任继愈则是冯友兰的侄女婿。20世纪三个最有名的中国哲学史家，围绕冯友兰的女性亲属，竟建立了一个有趣的三角裙带关系。）<br />
<br />
在任继愈学术生涯的最后二十年，他致力于主持各样大型古籍整理丛书，如《中华大藏经》（汉文部分）、《中华大典》、《国家图书馆藏敦煌遗书》等。客观地说，以任氏的学问、功力及认真，确是主持此等工程的上佳人选。<br />
 <br />
出没于上述这些文山字海中时，任继愈或会感到一种解脱。他在接受央视采访时曾回忆，“文科的学者们都是提心吊胆的”，又说“解放后马克思主义是立国的根本，可是真正的马克思主义哲学家却不多”。而他的儿子则告诉大众，任继愈要求在他死后，也不出全集。这是谦虚，还是自我否定？我忍不住想起阮籍的那句名诗：“终身履薄冰，谁知我心焦”。<br />
<br />
提心吊胆了数十年后，晚年的任继愈偶尔也会说一些令人惊讶的话。譬如他说：“旧社会所谓‘桐城义法’，写不出学术论文；用‘马列义法’装点的文章也多短命”。<br />
<br />
不论文章是否短命，任继愈毕竟得享93岁高寿，虽然民间对他的纪念，没有对季羡林那么轰烈。<br />
<br />
民间对任继愈有意无意忽视，对季羡林有意无意重视，其社会心理根源，在对一个有机知识分子和一个传统知识分子的价值判断，而不在他们的学问本身。事实上，如今大多数追悼二位逝者的作文，不过是借机跟学术，跟文化吊下膀子而已，并非一往情深。<br />
<br />
作为东方学专家，季羡林在专业领域内确实有相当贡献。而因为精通12类以上的语言，他又被看作与其师陈寅恪一样的天才，从而受到官方与民间的双重崇拜。但如其自言，他不是国宝，也不是泰斗，更不是国学大师。东方学根本就不属于传统国学范畴，他怎么会是国学大师呢？至于国宝，应当是我们拍了后又耍赖不要的圆明园兽首，或者是胖乎乎的大熊猫，或者是灰扑扑的兵马俑，怎么会是一个人呢？说到泰斗，季羡林的自谦也是一种自知。因为他要与整个20世纪的学术群星相较，而不是与自己的上海小瘪三徒弟比较。若与前者比，季羡林肯定不是最有分量的，若与后者比，季羡林则重得足以压死大象。<br />
<br />
在我看来，晚年的任继愈其实比季羡林更可爱。至少，任没有如季一样，高唱“政通人和、海晏河清”的艳歌，而任主持的《中华大藏经》，质量也远在季挂名总编纂的《传世藏书》之上――当然，前者是国家重大文化项目，而后者基本是商业项目，本不足比拟。而二者也有同样的不可爱，那就是都参与领头联合70人发表《甲申文化宣言》。这一宣言，被余英时、袁伟时等独立学者比拟为1935年1月，在国民党中宣部操纵下，十教授联名发表的《中国本位的文化建设宣言》。<br />
<br />
如果季羡林还活着，还能清醒思考的话，他或会吁请人们多纪念任继愈，就像1997年3月，他对记者说的那样：“我不是搞中国文化的，更不是搞哲学的。毛主席最推重任继愈。说中国文化、中国哲学，你们最好去找任先生。”<br />
<br />
