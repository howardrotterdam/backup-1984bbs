问：饱老师对李银河这段话怎么看<br />
<br />
李：“任何人都有和除了配偶之外的第三人发生性关系的权利。婚外情是错误的，只是因为它违反了婚姻法。至于一夜情，只要是单身，不仅有这个权利，在道德上也完全没有问题。所以对于一夜情的人，我给他们的建议只是做好防护措施，防止得病。”<br />
过于前卫大胆还是合理。。。？<br />
<br />
<br />
<br />
饱醉豚答：<br />
<br />
李银河这段话，既不前卫，也不大胆，更不合理。<br />
<br />
之所以不前卫，是因为自从有人类以来，几十万年都是这样干的，那种一夫一妻不许出轨的婚姻法不过数百年的历史，跟漫长的历史相比，不过是偶然的一些突发事件。<br />
<br />
之所以不大胆是因为李银河只提人和人发生关系，其实人和其他动物，比如牛、狗、羊，鸭，都可以发生关系，还可以跟植物发生关系，比如挖一个洞的大橘子，比如香蕉皮，比如切开一端的木瓜，比如女人跟萝卜和茄子性交。还可以跟非生物交配，比如充气娃娃，比如振动器。<br />
<br />
<br />
之所以不合理，是指李银河只承认权利――法律不禁止的自由，却不能上升到合法化的高度，没有提出立法保护婚外情，避免婚外情受到他人的诽谤和老婆老公以及其他人的干涉。也没有法律保护解决一夜情导致的争端问题，这些都是法律空白，需要有法可依，避免到时候纠缠太多。<br />
