&nbsp; &nbsp; 2010年4月1日，我在这里遭遇最“不堪”的愚人节！<br />
<br />
&nbsp; &nbsp; 4月1日下午15时，我随同刘云斌――山西疫苗事件报道中提及的“发烧18小时死亡”的刘紫阳父亲，前往太原迎泽宾馆。&nbsp; &nbsp; <br />
<br />
 上图，仅仅活了8个月的刘紫阳；下图，剖腹产下这唯一的孩子后，妈妈即做绝育手术。<br />
<br />
&nbsp; &nbsp;此上下照片，均系中国经济时报记者王克勤2009年11月17日，在山西省长治市长子县南陈乡善村刘紫阳家采访时所拍摄的。 <br />
<br />
&nbsp; &nbsp;详细报道请看：<a href="http://wangkeqin.blog.sohu.com/147633715.html" target="_blank">http://wangkeqin.blog.sohu.com/147633715.html</a><br />
<br />
<br />
<br />
&nbsp; &nbsp; 下图：刘紫阳的爸爸刘云斌在迎泽宾馆门前说：“卫生部和山西省专家都在欺骗我们！”<br />
<br />
<br />
<br />
&nbsp; &nbsp;本报《山西疫苗乱象调查》发表的第二天，即3月18日晚21时17分，新华网即发表《还原“山西疫苗事件”的真相－－新华社记者关于“疫苗事件”的访谈》，此新华社报道中这样表述：“据患儿家长刘云斌反映，在此之前，一位自称是山西省疾病预防控制中心副主任的人，多次找过刘云斌，并告诉他：他孩子的死是由疫苗引起的。刘云斌说，今日网上刊登的所有关于刘紫阳死因的不正确说法，都是那位自称是省疾控中心副主任的人所为，与其本人无关。”<br />
<br />
&nbsp; &nbsp; 然而，该报道刊发的第13天，即3月31日，居住在深山沟里的刘云斌才在太原迎泽宾馆第一次见到“疫苗事件调查组”人员、第一次看到3月23日由山西省预防接种异常反应调查诊断专家组出具的《关于网络报道15名儿童的基本结论》、第一次得知自己的孩子刘紫阳已经被鉴定为：死亡与疫苗无关。<br />
<br />
&nbsp; &nbsp; <br />
<br />
&nbsp; &nbsp; 4月1日下午14时30分，背着一堆资料的刘云斌和我坐在前往迎泽宾馆的出租车上，不断询问我“能登记吗，可以给孩子公正吗？” 他不断搓着双手，紧张地问“不知道人家见不见我？”<br />
<br />
&nbsp; &nbsp; 15时整，迎泽宾馆八楼，之前赶来的《财经》杂志社记者孙滔在802室外徘徊，他说，刚才自己被“请”出802室，那里的工作人员称要讨论一下，让他先回避。他奇怪的是，里面的人在看电视，何以讨论？<br />
<br />
&nbsp; &nbsp; 802室房门紧闭，门上的“猫眼”在黑暗的楼道里闪烁！<br />
<br />
&nbsp; &nbsp; 刘云斌敲门，没有应答，询问楼道服务员，不肯定没人也不否认有人。<br />
<br />
&nbsp; &nbsp; 刘云斌开始面对802室“猫眼”陈述自己的情况，希望能予以登记。“我是刘云斌，刘紫阳的父亲，我是来登记的……”&nbsp; &nbsp; <br />
<br />
&nbsp; &nbsp; (备注：有录像，由于技术问题一直无法传上来。）<br />
<br />
&nbsp; &nbsp; 802室依然没有丝毫动静，我暗示刘离开“猫眼”范围，然后大声对着楼道说，我们走吧！<br />
<br />
&nbsp; &nbsp; 仅仅五分钟，802室出来一中年女子，昂首挺胸直奔电梯。我上前询问，为何不开门。该女子一语不发，拂袖而去。<br />
<br />
&nbsp; &nbsp; 之后刘云斌再度敲门，802室依然猫眼闪烁，大门紧闭。<br />
<br />
&nbsp; &nbsp; 刘云斌蹲在楼道里等，不断有外来人士走到802门前，并不敲门，只是“热心”询问我是谁，干什么的，然后离去。<br />
<br />
&nbsp; &nbsp; 一小时后，刘云斌沮丧地离开，“明早再来”。离开迎泽宾馆时，他转身对我说，“你就别和我一起了，这样可能可以登记上。” <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;山西专用标签三：“随心所欲”<br />
<br />
&nbsp; &nbsp; 山西政协宾馆620室―――遭遇太原市公安局人员盘问“为何随心所欲！”<br />
<br />
&nbsp; &nbsp; 4月2日上午，我接到线报――山西疫苗事件报道中提到的“不哭、不闹、不笑、不玩、不说、不会走路的“燕燕”――高奥学的父亲高二清及其家属已经来到太原，并强烈要求见我，澄清“被鉴定”事宜。<br />
<br />
&nbsp; &nbsp; 4月2日上午9时，我来到高二清及其家属被“安排”的山西政协宾馆620室门口，随同我一起上电梯的两位男士也在六楼下了电梯，看到我在敲620室房门，立即表情严肃地询问找谁。<br />
<br />
&nbsp; &nbsp; 得知我是来采访的，穿咖色皮夹克的男士表示，最好不要采访，并要求我出示证件。在“验明正身”后，这名自称太原市公安局的吴姓工作人员语出惊人！<br />
<br />
&nbsp; &nbsp; “你们不要太随心所欲了！”<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;我追问：“什么叫随心所欲？”他不回答，转而打电话请示，然后依然要求我最好不要采访。<br />
<br />
&nbsp; &nbsp; 正在纠缠之际，高二清从门内冲出，大声喊：“我们要见记者，我们要见更多的记者。”我亦当即表示，当事人有接受采访的意愿，采访合法。吴姓人员尴尬，遂同意我进门，只是又安排一名保安全程陪同。<br />
<br />
&nbsp; &nbsp; (备注：有录音，由于技术问题一直无法传上来。）<br />
<br />
&nbsp; &nbsp; 在交谈中，高二清陈述：4月1日被“安排”住在620后，发生的深夜“突袭”式鉴定，让他十分不解!<br />
<br />
&nbsp; &nbsp; 高二清告诉我，4月1日晚十时左右，他和妻子及父亲下楼去买东西，620室只有孩子高奥学和奶奶两人。谁知，就在他们离开的二十分钟里，完成了他们期待已久的“鉴定”全过程！<br />
<br />
&nbsp; &nbsp; “听说他们就拿个小锤锤什么的，来锤了下，摸了下，就说完了。”那十几个人走的时候留下一堆资料，是高奥学之前住院的证明和2008年的相关鉴定。在这些资料里，高二清第一次见到2010年3月20日由山西省大同市天镇县疾病预防控制中心出具的《网络报道接种疫苗异常反应儿童调查登记表》。鉴定结果显示：高奥学与疫苗无关。<br />
<br />
&nbsp; &nbsp; “他们为什么深夜来鉴定，刚好我们还不在？”高二清十分不解。<br />
<br />
&nbsp; &nbsp; 一小时后我离开620室时，发现身后似有人跟随。回头看到那名全程“保安”若隐若现，遂前往市中心“闲逛”3小时确认没有人跟随后，方回住地。<br />
<br />
