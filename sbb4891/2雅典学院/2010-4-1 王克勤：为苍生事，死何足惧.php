													<h2>王克勤05年采写的定州事件报道</h2>
						
						
																											<div id="postmessage_543446" class="t_msgfont">河北“定州村民被袭事件”调查<br />
<br />
王克勤<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;　　<br />
<br />
&nbsp; &nbsp; 记者在一个村民冒死拍下的录相片断里，看到如下场景：<br />
 <br />
&nbsp; &nbsp; 随着急促的喊杀声，可见许多头戴彩色安全帽、身穿迷彩服的大汉，在昏暗的晨光中手持钩镰枪、棍棒、铁锹等，冲向居住在窝棚区的村民，向许多手无寸铁的男女村民疯狂袭击。期间不时还传出类似爆炸的巨响，以及响亮的连发枪声，有村民应声倒地。 <br />
&nbsp; &nbsp; 一方面是大批男女村民在惨叫声中惊慌四处逃跑，另一方面是许多迷彩服大汉手持“武器”从后追赶。也有一些村民手持铁叉尝试反抗，其中有村民被打到在地，有数名迷彩服大汉以棍棒狂殴受伤倒地的村民，同时可以清晰地看到火光及白色烟雾冒出。 <br />
<br />
&nbsp; &nbsp; 这不是电影里的镜头，而是在此次袭击中胳膊被打断的一位村民冒死拍下的。 <br />
&nbsp; &nbsp; 这是６月１１日，端午节的凌晨发生在河北省定州市绳油村的一幕。 <br />
&nbsp; &nbsp; 此次血案致死６名村民，据绳油村村民统计，袭击造成约１００人不同程度受伤，其中有５１名村民在不同的医院里接受救治。 <br />
<br />
&nbsp; &nbsp; 征地引发惨案 <br />
<br />
&nbsp; &nbsp; 近日，记者来到位于定州市南２０公里处的开元镇绳油村调查采访。 <br />
&nbsp; &nbsp; 事发现场位于绳油村南５００米处的一块约４００亩的土地上，因这块土地被当地火电厂征用，计划要做堆放与处理煤灰的场地，因此称为灰场。村民告诉记者，灰场的最南边就是当天的打斗主现场，也就是窝棚区。 <br />
&nbsp; &nbsp; 记者看到这个窝棚区约有足球场那么大，周围被一条宽１米，深８０厘米左右的土沟包围了起来，村民称这条沟叫“防战沟”。窝棚区里散布着上百个低矮的窝棚，窝棚里有床与被褥等生活用具，像古代战场上的兵营。据村民介绍，他们认为电厂征地存在大量问题，因此村民们便日夜驻守在这里，守护土地、阻止施工。“平时有一两百人驻守在这里”。 <br />
&nbsp; &nbsp; 记者赶到现场时，那里依然有几十名村民。 <br />
&nbsp; &nbsp; 村民们首先向记者出示了一种长约两米的镀锌钢管，管的一头被斜角切割，呈尖锐刺头，稍后的管侧焊有镰刀，“这就是让我们最害怕的钩镰枪！”同时他们还向记者出示了被打断的锄头柄以及木棍、铁锹。地上还散落着好多灭火枪、灭火器，有的像小推车，一枚小型灭火器外观仿佛像手榴弹，尚未点燃。 <br />
&nbsp; &nbsp; 仍然留守在窝棚看护事发现场的当事者牛振宗指着左腿和左肩，“这些伤都是那天留下的”。 <br />
&nbsp;&nbsp;　村民的回忆勾勒出了当时的喋血场景。 <br />
&nbsp;&nbsp;　１１日凌晨４时许，从窝棚区的东南方与电厂相连的专用道路上，开过来５辆大轿车，一辆大卡车。村民赵建学说，每辆大轿车估计都坐有七八十人左右，大卡车是专门拉武器的。 <br />
&nbsp; &nbsp; ４点半，炮仗二踢脚响起来。“有人来了！有人来了！”随着一个当晚担任了望任务女村民的呼叫，牛振宗和当晚在现场的许多村民一样，拿着叉子冲出自己的窝棚，随人流往东南方向跑去。“黑压压一片人，有三四百人，都是一身迷彩，头戴安全帽。” <br />
&nbsp; &nbsp; 在今年４月２０日遭受一次袭击后，村民在这块土地的东南方向约６０米处挖沟断路，火电厂的水泥路只能修到沟边，并且在窝棚区周围，挖了“防战沟”，并在窝棚区四周设置人员夜晚轮流巡逻值岗，４－５人一组，发现情况就放二踢脚，窝棚里人听到就拿自备的叉子出来“迎敌”，村里人抓紧赶过来救援。 <br />
&nbsp; &nbsp; 村民们知道要发生什么，这样的事情他们不是第一次遇到，不过这次规模最大。“刚开始，扔砖头和土块，想赶他们走。” <br />
&nbsp; &nbsp; “砰！砰！”低沉的枪声打破了僵局，村民没有留意对面“一字长蛇阵”两边闪出１０余支双管猎枪。 <br />
&nbsp; &nbsp; “不好，他们有枪，我们打不过，快跑”，５３岁的牛占京当时就叫喊村民躲避。 <br />
&nbsp; &nbsp; “冲啊！”，对方一个个跨过了深沟，一路掩杀过来，灭火器、灭火枪制造的弥漫中充满着血腥，“当初挖沟是怕人家来窝棚破坏，而这次深沟是自己这么多人受伤的重要原因，很多人就掉在沟里，跑不过去。” <br />
&nbsp; &nbsp; 进攻者把节节退却的村民一直往正西追了近一公里，然后又从背后包抄了赶来救援的村里的人，牛占京８１岁的叔叔就是在这次“救援”与“反包抄”中被打伤的，１００多辆被砸的自行车散落在村子到窝棚区的道路旁，还砸毁了近１０辆摩托车，并焚烧了１辆。 <br />
&nbsp; &nbsp; 这是一场不对等的“战争”。一边是铁叉、砖头、土块、深沟的农民，一边是带着猎枪、钩镰枪、木棍、匕首、灭火枪、灭火器、灭火罐的“进攻者”。“战斗”持续了大约１小时，结果是村民６死、５１重伤、１００多人不同程度的轻微伤，村民不完全统计认为。 <br />
&nbsp; &nbsp; 侯同顺是第一个在这场奇袭中死于非命的村民。其父侯臭和，７８岁，老泪纵横：“惨啊，刀子插到胸口，还在里面搅了搅，然后用铁钩子拉了百十米远。” <br />
&nbsp;&nbsp;　“大部分伤者我们送到临近的新乐人民医院去了，人家１２０到得快呀，定州的１２０半天才到。”牛占京对此很不理解，“定州路程更近呀”。 <br />
&nbsp; &nbsp; “当天６点已经报案了”，牛振宗对于办案效率表示怀疑，“这么重大的案子，村民要是不自动保护现场，早破坏光了。”对于一位自称是河北省公安厅干警的警察，“作为公安部门，１４日才介入这个案子进现场取证”，他非常不理解。 <br />
&nbsp; &nbsp; 对于这次发生的血案，拖着断臂和腰伤，从定州市人民医院专程赶回家见记者的牛占中说：“我们早就接到线报，说６月１０日国华定电要在这里继续施工，早晚还少不了一场恶战。” <br />
&nbsp; &nbsp; 他在这次对村民的袭击中，成功但艰难地拍摄了大约有５分钟的血腥场面。“我今年三月份在石家庄买了一台松下摄像机，目的就是为了留下一点证据。过去我们挨打，总是留不下证据，这对我们很不利。” <br />
&nbsp; &nbsp; 在１１日的混战中，他一开始忙着拍摄袭击场面，后来有人来追，就一直往西跑，但终于跑不动了，被六个人追上乱打，三两分钟后就昏倒了，摄像机也被打坏了。 <br />
&nbsp; &nbsp; 他对４月２０日那次有组织的袭击村民事件而忘记了带摄像机很遗憾，但幸好抓到了对方一个人。 <br />
&nbsp; &nbsp; 村民抓到的这个“人质”叫朱孝瑞，被关在绳油村村委会院子里的一房间里。朱孝瑞在接受中国经济时报采访时说，自己是安徽太和县人，２４岁，在北京打工３年。４月１９日中午１２时左右，接到郝红强（音）电话，说是去河北溜达一圈，给１００元。朱和郝是２００４年在夜总会做服务生时候认识，郝常去，经常给他一些五元、十元的小费，他感觉郝是个在社会上混的大哥。郝１．７６米左右，２８岁左右，挺胖，大约１７０斤。常在北京的永定路附近出没，手机经常换号。朱孝瑞说，现在看来，郝挺奸的。 <br />
&nbsp; &nbsp; 朱孝瑞回忆说，４月１９日晚上８点左右，２０来人坐上郝红强租来的中巴车，夜里１２点多，到达定州高速公路出口，郝联系后，一辆当地的红色夏利出租车带队，在一个饭店门口停，安排吃饭，大约２０分钟，饭后继续行使１５分钟，两车都灭掉车灯，从夏利车下来一个背影１．７４米左右，微胖的人打开后备箱，郝红强从里面拿出两捆８０厘米长的钢管，人手一根，告诉大家一个工地有人闹事，去帮忙处理下。朱孝瑞很后悔，“我也是农村人，早知道打的是农民，说啥也不去贪这１００元啊。” <br />
&nbsp; &nbsp; １０分钟后，夏利车停下来熄火，说到了，在拐弯处等，亮灯就走。 <br />
&nbsp; &nbsp; 下车后见人就打，后来因村民很多，看打不过，就跑。朱孝瑞说，“逃跑时，被绊倒抓住了，很多人从我身边跑，我叫他们拉我一把，没有人理我，郝也从我身边跑过，也没顾得上扶我一把”。朱孝瑞被抓后一直关在窝棚里，１３日被转移到村委会。“平常没有受到虐待，大爷大娘吃什么给我什么，对我很好，也让我给家里打电话。” <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 村委会变成了大灵堂 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp; 绳油村村委会大院在村子中心位置。 <br />
&nbsp; &nbsp; 这个大院如今经成为此次喋血事件中死亡者的灵堂。一幅一米见方的白布上，黑体大大的“冤”字在村委会大门横梁上悬着，在风中卷起，正对门口的７间正常作为村干部办公场所，临时作为了停尸间，１０米长，０．５米宽的大横幅上写着“为了贯彻中央土地政策维护村民利益他们献出保贵生命”。 <br />
&nbsp; &nbsp; 停放在村委会的６具尸体是村民从医院里“抢”过来的，除侯同顺与赵英志分别在现场和医院做了尸检外，其他４具当时没有做尸检，此后，是由公安机关来村里做的尸检。记者问他们为什么要“抢”尸体，村民牛巧柱道出了村民抢尸体的秘密，“怕被火化了，没有了证据”。 <br />
&nbsp; &nbsp; 这些天来，村民们组织村民一直在进行着吊唁仪式，村里的大喇叭里不断播放着哀乐。 <br />
&nbsp; &nbsp; 据家属介绍，亡者分别是： <br />
&nbsp; &nbsp; 第一个被打死的村民――侯同顺，男，５６岁，当场死亡。平时比较积极参与村民维权活动。 <br />
&nbsp; &nbsp; 被打死的最老的村民――牛同印，男，６０岁，“下身中刀，阴囊破裂，大腿根大动脉出血过多未到医院就死亡，后背也有刀伤。”４月２０日后，夫妻俩负责给“人质”朱孝瑞做饭，送饭。 <br />
&nbsp; &nbsp; 被打死的最小的村民――牛顺林，男，２６岁，“头部被打烂，左胳膊有刀伤，肚子中刀。”计划今年结婚，兄弟３人中的老小。负责看守“人质”朱孝瑞。 <br />
&nbsp; &nbsp; 创伤最惨重的村民――赵英志。男，５０岁，“整个是个血人，血肉模糊，浑身到处是骨折、到处有流血。” <br />
&nbsp; &nbsp; 自村里前来救援的死亡村民――牛占保，男，４６岁。“在赶过来的路上被枪打着了，发现时倒在灰场的乱坟岗边上。” <br />
&nbsp; &nbsp; 全家伤亡最惨重的村民――牛成社，男，４９岁，“头被整个打烂，两侧各有一个苹果一样大的深坑，失血过多死亡。”他一家是一死三伤，二弟牛成乱“头顶有个洞，左腿断了”，现在医院救治；他７５岁的父亲自村里赶过去时“前额被一钢钞打中”；他７４岁的母亲与他父亲同行时“腿与胳膊都被打肿。”１９岁女儿瑞卿刚刚参加完今年的高考，１６岁的儿子海锋的生日正好就是他父亲的忌日。 <br />
&nbsp; &nbsp; 据村民称，现在在新乐医院住院的伤者有３４人，其他的还有很多很多在石家庄、保定、北京、曲阳等地医院治疗。在定州医院治疗的仅仅有１２人。记者曾到定州市人民医院采访调查。被值守警察告知“特殊时期，没有路条不能采访。” <br />
&nbsp; &nbsp; 其实，在村民有了惨重伤亡的同时，前来袭击的“迷彩服大汉们”也有伤亡。最近有媒体报道说，据可靠消息，袭击人员有一人死亡，事发当日，有人看到，一辆车将４个身着迷彩服的人送进定州人民医院。 <br />
&nbsp; &nbsp; 那么这么多“迷彩服大汉们”是受何人指使的呢？ <br />
&nbsp; &nbsp; ６月１８日下午，新华社发布消息称，“在定州市绳油村附近的国华定洲电厂灰场建设用地上，约３００余人持械袭击在现场聚居多日的数百名绳油村村民。现已初步查明，事件是由定洲电厂灰场工程承建人张某及其丈夫甄某等人策划组织的，目前包括张某、甄某在内的２２名犯罪嫌疑人已被抓获归案。” <br />
<br />
<br />
&nbsp; &nbsp; 都是征地引发了矛盾 <br />
<br />
&nbsp; &nbsp; “这个群体事件是征地引发的矛盾。” <br />
&nbsp; &nbsp; 定州市委宣传部部长任振焦对记者如是说。 <br />
&nbsp; &nbsp; 为什么这么多村民如此持久且非常惨烈地对抗电厂灰场的施工呢？ <br />
&nbsp; &nbsp; 这得先从火电厂征地说起。 <br />
&nbsp; &nbsp; 一份有关部门提供的材料显示，河北国华定洲发电有限责任公司（以下简称火电厂）是２００１年动工、２００４年投产的大型火电厂，目前拥有两台６０万千瓦发电机组，是国家“十五”重点项目。 <br />
&nbsp; &nbsp; 火电厂就建在离绳油村不到两公里的地方，绳油村村南的这３７９亩土地便被相关部门规划为火电厂的煤灰堆放处理场。 <br />
&nbsp; &nbsp; 据村民们介绍，这块位于村南的土地，由三部分组成：一是有８０亩属于村里的果园，原有近３０００棵成年梨树；二是有２００亩承包给村民个人的林地，树龄在两三年以上的杨树；三是有近１００亩麦田，“是我们村民的责任田。” <br />
&nbsp; &nbsp; 据定州市解决灰场问题工作组２００４年８月２７日《致灰场聚集人员的一封公开信》称，火电厂“２００２年９月７日就依法取得灰场使用权，绳油村的每一位群众也已经得到合法的补偿。” <br />
&nbsp; &nbsp; 但是，村里的许多村民却说，“那时候我们什么都不知道，根本不知道征地的事情。”遗憾的是，被定州政府部门认为火电厂合法取得的灰场土地却在２００３年底的第一次施工中便遭遇了绳油村全体村干部的反对，“村干部带头罢工（村民的话，意思是阻止施工）”，灰场施工第一次受阻。 <br />
&nbsp; &nbsp; 村支部委员、村会计牛英奇告诉记者：“当时说好，一亩地是一万五千多元，先期给我们村里１５０万元，但是后来只给了１００万元。我们去市里要钱，市里说已经给了镇里，镇里却说没有。我们也没办法。”“最后我们村支部、村委召开会议，大家一致认为，如果钱不给，就不让施工。”他补充说：“那个时候，老百姓还没有罢工呢。于是我们村支委、村委带头去罢工。” <br />
&nbsp; &nbsp; 他接着又补充说：“我记得是春节前，村里的全体干部去灰场拦他们的施工车。” <br />
&nbsp; &nbsp; 其实在干部罢工前，征地的事情已经牵涉到了不少村民。 <br />
&nbsp; &nbsp; ２００３年５月，市、镇、村的干部要量牛俊民等村民的地。共产党员牛俊民告诉记者：我家在灰场那里有１．８亩责任田，说是要征地，我没有同意，不让他们量地。当时好多人都不同意，因为我们什么都不明白，给我们没有个交代呀。于是村里召开在灰场有责任田农户的会议，要强行征地，说不让征不行，这是国家要征地。 <br />
&nbsp; &nbsp; 到这一年９月的时候，村会计给牛俊民一张票据，让他到县农业银行取钱，“给了大约两千元青苗款”。 <br />
&nbsp; &nbsp; 牛俊民说，其实干部那次罢工后，不久人家依然在施工，村里很平静。许多村民还到正在施工的果园去拣柴禾。但是，进入２００４年情况就有了新的变化。 <br />
&nbsp; &nbsp; 村民的第一次“罢工”。牛俊民说：大约是腊月十几的一天，施工车开进了我们那片杨树林，杨树不是村里的，是家家户户自己栽的，两三年的树一棵才赔一块二，太亏我们农民了！那天人家的推土机推了我们四五十亩林地，有树的村民哪个不心疼！都赶到那里，阻止他们施工。 <br />
&nbsp; &nbsp; 牛俊民说：开始，我妈上前拦，他们推开了。后来，我去拦，村干部对我说，你不能拦，这样你没有好果子吃。 <br />
&nbsp; &nbsp; 最后，村民来的很多，施工只好停了下来，这是灰场施工第二次受阻。 <br />
&nbsp; &nbsp; 几天后，便是春节了，灰场征地的事情于是便成了绳油村村民们这个春节最大的话题。 <br />
&nbsp; &nbsp; “我们一定要讨个说法，弄个明白” <br />
&nbsp; &nbsp; ２００４年春节刚刚结束。 <br />
&nbsp; &nbsp; 正月十九，施工的工程车来了，还来了镇里的干部。近５００多名村民赶到灰场就树苗赔偿的事情与施工人员、镇村干部争论。 <br />
&nbsp; &nbsp; 牛俊民说，“你买个瓜子也得交钱呀，你不能白拿呀！我们这是长了好几年的树啊”。 <br />
&nbsp; &nbsp; 当天７０多岁的牛书印老人被气得晕了过去，送进镇卫生院。 <br />
&nbsp; &nbsp; ３月９日，灰场来了８辆工程车要进行施工，村民们发现后赶了过去，把施工人员包围了起来。无奈的施工人员叫来电厂的一位老总。 <br />
&nbsp; &nbsp; 村民们看来了一个老总，于是纷纷发问：“你们占我们的地怎么一分钱都不给？”“到底给我们村多少钱？”在村民的追问下，这位老总说：“给你们４６００多万，还不行呀？” <br />
&nbsp; &nbsp; 电厂老总的这句话使整个事件发生了一个巨大的变化！ <br />
&nbsp; &nbsp; 一些村民当场对大家讲，镇里只给村里才５００多万元，由４０００万元到５００万怎么一下少了这么多？ <br />
&nbsp; &nbsp; 村民说：“这么多的钱被上面给占了、贪了，这还了得！” <br />
&nbsp; &nbsp; ８２岁的村民牛建国告诉记者：“我们觉得被愚弄了，全村只有２０００亩土地，征这么多土地，我们村民都不知道，我们认为征地不合法！” <br />
&nbsp; &nbsp; 当天在赶走施工人员后，村民们来到村委会，进行集资，要上访咨询。“我们要讨个说法，要问个明白！” <br />
&nbsp; &nbsp; 村民提供给记者的一份材料上是这样写的： <br />
&nbsp; &nbsp; （２００４年）３月１１日，村民牛国奇等二人从村里搭车赶往省城要去找律师咨询，汽车路过镇政府时，二人被扣留，全部材料被收走，因村民强烈要求才放回。 <br />
&nbsp; &nbsp; ３月１２日，牛国奇等６人被叫到镇政府，下午政府不想放人，村民１０００多人在大队门前不让（拦住了镇政府的一辆车），在６点多钟才把６人放回。 <br />
&nbsp; &nbsp; ３月１５日，市委副书记赵国军亲自带队，包括公安局以及其他政府工作人员施工人员约２００多人，铲车８辆到灰场强行施工，有１２００多村民赶到现场，不让施工。 <br />
&nbsp; &nbsp; ３月中旬，有１０辆警车，６辆卡车，鸣笛从绳油村街道通过。 <br />
&nbsp; &nbsp; ３月１９日，来了铲车两辆，挖掘机两辆，村民去了１０００多人，他们退出。 <br />
&nbsp; &nbsp; ４月７日，原市委书记和风来绳油村，上千人跪地诉冤，他却说，“这场面我见多了！少来这套”。 <br />
&nbsp; &nbsp; ５月底，政府为了缓和与村民的矛盾，由镇政府组织全体村民选举产生４０名村民代表，但从未和村民代表进行协商。（注：据村民介绍，村里原有的２４名村民代表，已于２００５年２月被镇派村支书全部撤职。） <br />
&nbsp; &nbsp; ７月３日凌晨，村民代表牛烟平、侯不止、牛顺喜、牛俊敏遭到不明身份的人袭击，幸好被闻讯赶到的数十名村民救出。 <br />
&nbsp; &nbsp; ７月５日，村民代表牛才民被骗到开元镇电信局交电话费，一去不回。７日晚９时接到通知说他在派出所。８日上午牛才民儿子到派出所，拒绝在不符合日期的拘留证上签字。中午，牛才民妻子被刑警队赤脚拉走。 <br />
&nbsp; &nbsp; ７月９日，村民牛旭光被抓；１７日前后，牛同顺被抓，五天之后家人才知道。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;　　…… <br />
&nbsp; &nbsp; 村民代表牛占中说：“六七月份，我们新当选的村民代表人人心惊胆颤，那一阶段不断地抓村民代表，我们害怕呀！” <br />
&nbsp; &nbsp; 他说，自２００４年３月份到７月９日，对方共组织强行施工１０余次，出动工程车辆５０台次，警车８０台次，公安及施工人员５０００余人次。 <br />
&nbsp; &nbsp; 在施工过程中，共打伤了５位老人。毁掉成年梨树３１４０多棵，大小杨树１０００多棵。 <br />
&nbsp; &nbsp; 这一系列的冲突皆因为村民每当电厂施工的时候就去阻拦。村民代表赵建学介绍说，村民采取的办法是，老年人和妇女坐在施工机械前面，年轻人在后面。<br />
&nbsp; &nbsp; 时间久了，不知道何时形成了一个不成文的做法，只要发现了有人来施工，就放二踢脚炮仗，大家听到，全村人就到地里静坐。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;<br />
&nbsp; &nbsp; 搭建窝棚区，驻守“罢工” <br />
<br />
&nbsp; &nbsp; ２００４年７月９日，对于绳油村村民的“罢工”行动是一个转折与升级的一天。 <br />
&nbsp; &nbsp; 村民牛建国、牛建章等人介绍说，那天凌晨２点半，定州市直属机关出动了大小车辆２００余台，施工人员等３０００多人，来到灰场要进行施工。全村许多村民或在村里或在灰场与施工人员进行对抗，直到下午１７时，当天晚上，许多村民将自家的农用三轮车开到灰场驻守在那里不走了，还有不少村民干脆睡在地上。 <br />
<br />
&nbsp; &nbsp; 这一睡，村民的“罢工”行动便变换了一种方式。 <br />
&nbsp; &nbsp; 此后不久，他们便在如今窝棚区搭起了几十座简易塑料棚，正式驻守灰场地，开始了一场持久的“罢工行动”。 <br />
&nbsp; &nbsp; 随着天气一天天变冷，“盖两床被子都冻醒来”。 <br />
&nbsp; &nbsp; 村民们便就地取材，挖地两米在这个区域建设了一个窝棚区。并且制定了一系列防守办法，不论白天黑夜，时刻安排村民轮流值班。每天晚上在那里至少有一两百人驻守在窝棚区。 <br />
&nbsp; &nbsp; 村民代表赵建学补充说，“之所以要舍下家小，住在荒野，是因为，当时形势危急，呆在家里，有可能被一个一个的抓走，我就给大家说，咱们都躺在地里，守住我们的３７９亩地。于是，大家都去了。” <br />
&nbsp; &nbsp; 赵建学说，“自此后，村民携带被褥、帐篷来到这３７９亩地上，搭起了３００多个帐篷，许多村民聚集居住在这块土地上，一直到现在，包括大年三十也在这里。” <br />
&nbsp; &nbsp; 村民赵建学说，其实政府后来也想来软的。 <br />
&nbsp; &nbsp; 村民给记者提供了一份原市长郭振光春节前在凛冽的寒风中来到这块土地上慰问村民的录像。 <br />
&nbsp; &nbsp; 郭市长很同情村民：“今天呢，来这里看望看望大家。所以各位父老乡亲，一定依靠我们的党支部，相信我们解决问题的小组，我受保定市委和定州市委的委托，全权处理这个问题，希望我们把心态放平。在所有的村民达成一致意见的前提下，如果谁又施工进场，我带领绳油村的全体村民来这里和他对抗。如果我不来，我也不称为市长。不管我们村民是不是晚上在这，是不是白天在这，当然我不强加于父老乡亲。相信我，不要在这里睡觉，特别是这些老人在这里受罪。我不是强迫大家。在没有解决问题之下，这里谁也不能进来，包括施工的……放心，以后有什么事情托给我，我会经常到你们村来。向父老乡亲表示歉意，对你们受的这些罪过，我做一个检讨，我作为一个市长，向大家赔礼道歉，我给大家鞠个躬。” <br />
&nbsp; &nbsp; 村民给记者的一个录像资料上还显示：在窝棚区，村民们还打上了“惩治腐败，维护土地权利”等类似的横幅。 <br />
&nbsp; &nbsp; 村民说，其实，这正是村民之所以不断“罢工”的另一个重要原因，征地之事只是把村民对该村领导的不满点燃了。记者走访的几十个村民，都认为前两任村支书存在利用职权贪污浪费村集体财物的问题。为此，他们还给记者提供了相关的反映材料。他们就村干部的腐败问题曾向上级机构不止一次的反映，但没有结果。 <br />
&nbsp; &nbsp; 赵建学说，想解决绳油村的问题，必须让我们全村人８０％以上的人同意了才可以；还要解释为什么不给我们下达中央一号文件；征地赔偿，整体上程序有问题；对村干部我们有看法，修铁路的挖土方款下落不明，几百万的酒厂卖光了，我们也不知道到哪里去了，我们只是求一个公道。 <br />
&nbsp; &nbsp; ２００４年１０月２２日，村里１８０多人，前往北京上访，被定州政府部门接回。分别被安排到市直各机关对他们开展耐心细致的思想工作。 <br />
&nbsp; &nbsp; 但这次到北京上访，赵建学说，“我没去，怕扣帽子。” <br />
&nbsp; &nbsp; 但他在这一天还是被抓进了公安局。一直到２００４年１２月３１日才放出来。赵建学说：“我出来后一检查出了一大堆的病，给市公安局局长肖福弟打电话，她答应给我报销医药费，但后来又给我退了回来。” <br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;　　党员牛山林也和市委副书记赵根军相互沟通过。“这个事情不该发展到这个地步，我们全村都动了感情，真是英雄的人们。多少年来，我们都是模范村，不管是在交公粮还是计划生育工作上都是顶呱呱。我们现在已经不是为了这一点钱，而是要把事情搞清楚，还我们一个明白。” <br />
&nbsp; &nbsp; 他说曾经给赵根军哭诉过：“老百姓一冬天都住在地窖里，而你们政府就知道打压，伤了老百姓的心。” <br />
&nbsp; &nbsp; 据他讲，这几年来，被公安部门带走的村民有二三百人次。有时候，公安经常用大钳子把锁剪开，进门搜上访材料。 <br />
<br />
<br />
&nbsp; &nbsp; 几个焦点问题的不同声音 <br />
<br />
&nbsp; &nbsp; 有几个问题始终是此次征地中的关键问题：此次征地在村里有没有公示？到底有没有通过村民代表大会？征地过程中与村里有没有签订合同等等。 <br />
&nbsp; &nbsp; “第一次冲突后，村里开始通知我们去领钱，但没说什么钱。”赵建学说，他是第一个去领钱的，前七名去领钱的都是摁了手印，每人２０００元。但后来，再去领钱的时候就开出了条件“不允许影响电厂施工”。 <br />
&nbsp; &nbsp; 条件开出后，大致有２／３的家庭就不去领钱了。随后，村干部、乡干部、市干部给每家送款。并给大家发了“明白纸”――《定州市支电办关于定州电厂占地有关情况的说明》：<br />
&nbsp; &nbsp; 定州电厂共占地１７４８亩，全部征地费用５９２９万元，每亩征地费用３３９１９元。按照有关政策规定，此项费用需上交： <br />
&nbsp; &nbsp; １、有偿使用费每亩６６６６．６６元。 <br />
&nbsp; &nbsp; ２、耕地开垦费每亩６６６６．６６元。 <br />
&nbsp; &nbsp; ３、南水北调费每亩１７６０元。<br />
&nbsp; &nbsp; ４、征地管理费、按照征地费用总额的３％计算，平均每亩１０５５元。 <br />
&nbsp; &nbsp; ５、土地评估费每亩１０３元。 <br />
&nbsp; &nbsp; ６、耕地占用税每亩３３３３元。 <br />
&nbsp; &nbsp; 以上６项费用共上交国家、省市费用每亩１９５８４元。电厂灰场和灰场路共占用绳油村土地３７８．９９４５亩，征地款５８６．６９万元。 <br />
&nbsp; &nbsp; 记者查阅了财政部网站，在２００２年全国已经取消了征收南水北调附加费。 <br />
&nbsp; &nbsp; 国土资源部执法监察局副局长常嘉兴在接受记者采访时表示：政府和电厂只能签订土地出让合同，电厂是依法用地，手续要合法，政府需要在土地开始征用前要明确，接着，政府要进行公告，凡是涉及到土地的村民必须了解土地的用途，补偿标准要明晰，这个补偿要经过村民同意，如果两次公告后，村民不同意，也不能征用，要进行听政会，只要有村民拒绝接受赔偿，这个土地征用过程就属于程序违法。 <br />
&nbsp; &nbsp; 而村民认为，有关部门贪污、截留了绳油村的征地补偿款。 <br />
&nbsp; &nbsp; 村民们认为，首先是征地未召开村民会议或村民代表会议决定，这是《村民委员会组织法》和河北省有关法律规定的这类事情“必须要提请村民会议讨论决定”。 <br />
&nbsp; &nbsp; 其次，征地及其征地安置补偿方案定州市政府未依法公告，听取农民的意见。 <br />
&nbsp; &nbsp; 另外，政府的“说明”与《土地法》的规定并不一致：土地补偿费、安置补助费和青苗补偿费不包括电厂应当向各级政府交纳的有关费用。 <br />
&nbsp; &nbsp; 根据《土地法》的规定，应该按照“需要安置的农业人口数”计算安置补助费，而不是按照“每亩支付安置补助费６１９２元。” <br />
&nbsp; &nbsp; 定州市委宣传部部长任振焦对记者出示的一份“看了什么都明白的”的“绳油村问题真相”的一张纸上显示：原村主任牛全占说，两委干部、党员、村民代表会都开了，并有会议记录。 <br />
&nbsp; &nbsp; 这样的会议记录是否有做假的可能？曾经当过这个村工作组小组长的定州市委宣传部副部长黄宏京说：“我们去村里做了大量的工作，但是他们就是不听，如果这个记录是假的，那我们就没办法了。” <br />
&nbsp; &nbsp; 但记者随机采访了村里的几位党员，从石家庄电务段退休的党员牛山林、牛喜林都表示从未参加过这样的会议。<br />
&nbsp; &nbsp; 在２００２年底，政府确认的依法征地时间里，当时的村民代表侯平均、牛占中等人均对记者讲，他们没有参加过关于参与讨论征地的村民代表会议。８２岁的村民牛建国给记者出示了一份时任村民代表的１９人（共计２４人）的一份共同摁了手印的声明：“电厂征地之事未开过村民代表大会。” <br />
&nbsp; &nbsp; 此次征地有没有征地合同？ <br />
&nbsp; &nbsp; 村民们都发出同样的疑问。就此问题，记者采访了村支委委员、会计牛因奇，他说，自始至终，没有和村里签过合同。这些年，都是这样征的，从来不签合同。 <br />
&nbsp; &nbsp; 记者在村里没有找到其他村干部，但此说法被开元镇东沿里村支书王茂林所证实，他们村在此次电厂征地中比绳油村征的更多，但依然没有签任何合同。<br />
&nbsp; &nbsp; 为什么国华定电不能直接和村民谈判？定州市委宣传部副部长黄宏京解释说：“按照政策要求，首先是政府和用地单位谈判，用地单位把土地补偿款首先给我们市财政，然后由市财政根据不同的情况决定给予村民多少。” <br />
&nbsp; &nbsp; 在村里采访的那几天里，记者一直找不到过去的村支书记与现任村支书，也找不到过去的几任村主任与现任村主任。村民说，干部们早就跑了，哪里还有一个在家。牛山林对村干部也颇失望，“党支部决定一切，但村里则由四五家村民控制了。” <br />
<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 定州缘何如此青睐火电厂 <br />
<br />
&nbsp; &nbsp; 为什么定州市政府这么支持这一项目，定州市委宣传部给记者的一份《绳油村问题真相》第一段这么讲述的：“２００１年１０月２５日，定州电厂开工庆典……历史不会忘记，人民不会忘记，１３年求索路上的艰辛，１３年望眼欲穿的渴盼――荫庇后代子孙的一件大事今日梦圆。” <br />
&nbsp; &nbsp; ２００４年１１月１７日的《定州日报》头条报道中定州市发展计划局局长何福顺这样说，“定州电厂是国家级大项目，对我市经济具有第一拉动作用，项目完全竣工后，年产值可达到４０亿元左右，税金４亿元左右。仅明年可实现税金２亿元，可占财政收入的２５％左右。电厂建设可以改写定州的历史，可以改变百万人民的命运。” <br />
&nbsp; &nbsp; 在此篇文中，开篇写道：“几天来，全市各级各部门继续对绳油村部分不清真相村民在灰场非法聚集表示愤慨，纷纷声讨个别别有用心者的不法行为。” <br />
&nbsp; &nbsp; 记者看到的定州市委、市政府２００４年４月１０日的《关于优化发展环境　加快电厂灰场建设的通告》也证明了在过去政府和部分村民之间矛盾已经无法调和：“全村５２０户有５１８户支取了征地补偿款，在全村正气不断上升，邪气不断下降的时候，个别幕后操纵者又在制造谣言，蛊惑群众。征地款发放是经过村民代表会议通过的，造谣者的险恶用心，就是要制造混乱。当前，保证电厂如期发电，保证灰场建设尽快入场建设，迫在眉睫，如果影响了灰场的进场施工和电场正常发电，定州百万人民也不答应。我们郑重宣布：任何企图以无理要求，甚至过激行为阻挠灰场的行为都是违法的，对那些胆敢以身试法，破坏定州发展环境建设的人，我们将严厉打击，绝不手软。” <br />
<br />
&nbsp; &nbsp; 定州官方：追捕凶手，也要追究村民个别人的违法行为 <br />
<br />
&nbsp; &nbsp; ２００５年６月１４日下午，定州市委宣传部会议室。<br />
&nbsp; &nbsp; 定州市委宣传部部长任振焦首先对记者表示，“此前的北京某家报纸的报道是失实的，对党和人民极不负责任的。其中引用《河北青年报》的报道也是以讹传讹，诱导群众。”<br />
&nbsp; &nbsp; 但这篇报道具体到哪些细节有问题，这位部长一直没有回答。 <br />
&nbsp; &nbsp; 对于“６・１１”案件的看法，任振焦表示，“老百姓首先是受害者，但他们前因是违法的，国华定电已经取得了合法的土地使用权，村民们阻挠已经构成了扰乱社会公共秩序罪。他们不离开这块土地，目的是想要更大的补偿。其实是在补偿问题上，已经很照顾他们，按照基本农田的补偿标准给予了村民。为什么不能把所有的钱都给村民，这譬如我们创造了１００元的价值，但工资就是３０元这样一个道理。” <br />
&nbsp; &nbsp; 对于这件案件的进展，他给了记者一份《关于“６・１１”案件的简要情况》。根据这一材料的介绍，“已经按照有关政策法规对村民进行了补偿，并已经发放到位。但是极少数别有用心的村民私欲膨胀，制造谣言，蛊惑群众，鼓动部分村民在灰场阻挠施工，搭建窝棚，昼夜聚集，达１４个月之久，致使电厂４００多吨的粉煤灰无处存放，严重影响了电厂的正常生产秩序。更有甚者，还利令智昏，两次煽动、组织部分村民到北京上访，并冲击中南海、新华门，其中首要分子和少数闹事者已经构成聚众扰乱社会秩序罪。在此期间，定州市委市政府多次派出工作组，深入现场做耐心细致的思想政治工作，但都无济于事，问题始终无法解决。” <br />
&nbsp; &nbsp; 此前已经有邢会强等三人因聚众上访分别被判２－３年有期徒刑，而绳油村“６・１１”血案出了以后，政府将对绳油村采取怎样的解决途径？ <br />
&nbsp; &nbsp; 定州市委宣传部副部长黄宏京对记者谈了他的看法：“首先是尽快破案，追捕凶手，但还要追究个别带头闹事者的法律责任，他们聚众上访闹事，还有很多人没有被追究法律责任。” <br />
&nbsp; &nbsp; 而在６月１４日下午，定州市委宣传部专门放给记者的一个电视专题，他强调，“看了这个专题片什么都明白了”。片中说，“个别人目无法纪，不与党支部、村委会、工作组配合，利令智昏，随意提出非分要求，甚至任意编造谎言，煽动蛊惑群众，与政府和全市百万群众对抗，已经达到不能容忍、不可理喻的程度。” <br />
<br />
&nbsp; &nbsp; 定州市原市委书记、市长为什么被免职？是否免职就意味着一切问题就解决了？ <br />
&nbsp; &nbsp; 这位副部长说，该是谁的问题都会追究到底的。 <br />
<br />
