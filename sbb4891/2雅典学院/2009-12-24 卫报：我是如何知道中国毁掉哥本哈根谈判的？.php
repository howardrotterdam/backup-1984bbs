																											<div id="postmessage_393256" class="t_msgfont">　　刚刚翻译，难免有错，本人秉着客观中立的态度，尽量保证言语的准确性，文章仅代表原作者立场。如有疏漏请大家指出。欢迎转载<br />
<br />
　　How do I know China wrecked the Copenhagen deal?I was in the room<br />
<br />
　　我是如何知道中国毁掉哥本哈根谈判的？我在现场<br />
<br />
　　英国卫报 Mark Lynas<br />
<br />
　　翻译：KTO<br />
<br />
　　Copenhagen was a disaster. That much is agreed. But the truth about what actually happened is in danger of being lost amid the spin and inevitable mutual recriminations. The truth is this: China wrecked the talks, intentionally humiliated Barack Obama, and insisted on an awful &quot;deal&quot; so western leaders would walk away carrying the blame. How do I know this? Because I was in the room and saw it happen.<br />
<br />
　　哥本哈根无疑是一个灾难。但是灾难背后的真相很可能因为国家间不可避免的相互指责而永远不为人所知。事实是：中国毁掉了谈判，故意羞辱了Obama，并且坚持其所提出的糟糕的提议从而使西方国家领导人承担来自舆论的指责。我是如何知道这些的呢？因为我当时在谈判房间里目睹了这一切。<br />
<br />
　　China's strategy was simple: block the open negotiations for two weeks, and then ensure that the closed-door deal made it look as if the west had failed the world's poor once again. And sure enough, the aid agencies, civil society movements and environmental groups all took the bait. The failure was &quot;the inevitable result of rich countries refusing adequately and fairly to shoulder their overwhelming responsibility&quot;, said Christian Aid. &quot;Rich countries have bullied developing nations,&quot; fumed Friends of the Earth International.<br />
<br />
　　中国的策略很简单：阻碍公开谈判两周，并且保证使关门协议看起来像是西方国家的另外一次失败。可以十分肯定的是，救援机构，公民社会运动以及环境组织全都上了中国的当。Christian Aid说道：谈判的失败是“西方国家拒绝合理公平的承担责任的必然后果”。“富国威胁了发展中国家” C 愤怒的Friends of the Earth International这样说道。<br />
<br />
　　All very predictable, but the complete opposite of the truth. Even George Monbiot, writing in yesterday's Guardian, made the mistake of singly blaming Obama. But I saw Obama fighting desperately to salvage a deal, and the Chinese delegate saying &quot;no&quot;, over and over again. Monbiot even approvingly quoted the Sudanese delegate Lumumba Di-Aping, who denounced the Copenhagen accord as &quot;a suicide pact, an incineration pact, in order to maintain the economic dominance of a few countries&quot;.<br />
<br />
　　一切不出意料，但却完全与事实相悖。Even George Monbiot在昨天的卫报上错误指责奥巴马一个人。但是我看到的确是奥巴马在为挽救协议的签署全力以赴，而中国的代表确一而再再而三的将其否决。Monbiot甚至颇为赞赏的引用了苏丹代表Lumumba Di-Aping的话：哥本哈根协议是“一个自杀性，毁灭性的协议，其目的是为了保证少数国家的经济主导地位”<br />
<br />
　　Sudan behaves at the talks as a puppet of China; one of a number of countries that relieves the Chinese delegation of having to fight its battles in open sessions. It was a perfect stitch-up. China gutted the deal behind the scenes, and then left its proxies to savage it in public.<br />
<br />
　　Here's what actually went on late last Friday night, as heads of state from two dozen countries met behind closed doors. Obama was at the table for several hours, sitting between Gordon Brown and the Ethiopian prime minister, Meles Zenawi. The Danish prime minister chaired, and on his right sat Ban Ki-moon, secretary-general of the UN. Probably only about 50 or 60 people, including the heads of state, were in the room. I was attached to one of the delegations, whose head of state was also present for most of the time.<br />
<br />
　　然而，苏丹在谈话中完全变为中国的傀儡，并且与其他几个国家一起，使中国代表团免于在公开会议与他国进行辩论。这是一个完美的计划--中国在幕后说出其谈判条件，然后这些国家公开为其辩护。下面是周五晚上在24个国家的领导人关门会议上真实经过：奥巴马出席会议数小时，他的旁边分别是英国首相布朗和埃塞俄比亚首相Meles Zenawi。会议由丹麦首相主持，坐在其右边的是联合国秘书长潘基文。在会议室内算上各国领导，总过只有50-60个人。我安排到一个国家的代表团名下。<br />
<br />
　　What I saw was profoundly shocking. The Chinese premier, Wen Jinbao, did not deign to attend the meetings personally, instead sending a second-tier official in the country's foreign ministry to sit opposite Obama himself. The diplomatic snub was obvious and brutal, as was the practical implication: several times during the session, the world's most powerful heads of state were forced to wait around as the Chinese delegate went off to make telephone calls to his &quot;superiors&quot;.<br />
<br />
　　我在回忆中所看到的非常令人震惊。中国总 理 温 家 宝， 本人并没有出席会议，而是派出了外交部的一个二级官员与奥巴马面向而座。这个行为的暗示很明显并且粗鲁。以至于在会议进行中这些世界上最强大的国家的首脑甚至要等待中国代表离席以便给打电话向“上级”请示。<br />
<br />
　　Shifting the blame<br />
<br />
　　转移指责<br />
<br />
　　To those who would blame Obama and rich countries in general, know this: it was China's representative who insisted that industrialised country targets, previously agreed as an 80% cut by 2050, be taken out of the deal. &quot;Why can't we even mention our own targets?&quot; demanded a furious Angela Merkel. Australia's prime minister, Kevin Rudd, was annoyed enough to bang his microphone. Brazil's representative too pointed out the illogicality of China's position. Why should rich countries not announce even this unilateral cut? The Chinese delegate said no, and I watched, aghast, as Merkel threw up her hands in despair and conceded the point. Now we know why C because China bet, correctly, that Obama would get the blame for the Copenhagen accord's lack of ambition.<br />
<br />
　　有一个事实是那些指责奥巴马和其他富国的人所需要了解的：是中国的代表坚持要把工业化国家在2050年之前减排80%的目标从最终的条款中删除的。针对此事，默克尔愤怒的说“为什么连我们都不能提出自己的目标？”澳大利亚总理陆克文甚至愤怒的拍起了麦克风。巴西代表也指出了中国立场中混乱的逻辑。为什么中国连发达国家单方面减排的提议都不接受呢？现在我知道了：因为中国正确预测到了奥巴马会为哥本哈根协议的谈判失败承担大部分的指责。<br />
<br />
　　China, backed at times by India, then proceeded to take out all the numbers that mattered. A 2020 peaking year in global emissions, essential to restrain temperatures to 2C, was removed and replaced by woolly language suggesting that emissions should peak &quot;as soon as possible&quot;. The long-term target, of global 50% cuts by 2050, was also excised. No one else, perhaps with the exceptions of India and Saudi Arabia, wanted this to happen. I am certain that had the Chinese not been in the room, we would have left Copenhagen with a deal that had environmentalists popping champagne corks popping in every corner of the world.<br />
<br />
　　这之后，在印度的偶尔支持下，中国开始提出自己提案，在提案中2020年全球碳排放达到峰值（保证全球升温2摄氏度以内的关键一步）被替换成碳排放量应“尽快打到峰值”这样模糊的标准；2050年全球碳排放减半的长期目标被直接删除。也许除了印度和沙特阿拉伯，没有其他国家愿意看到这样的结果。我很确定当时如果没有中国人在会议室，我们会带着一个让环境主义者们欢呼雀跃的协议离开哥本哈根。<br />
<br />
　　Strong position<br />
<br />
　　强硬立场<br />
<br />
　　So how did China manage to pull off this coup? First, it was in an extremely strong negotiating position. China didn't need a deal. As one developing country foreign minister said to me: &quot;The Athenians had nothing to offer to the Spartans.&quot; On the other hand, western leaders in particular C but also presidents Lula of Brazil, Zuma of South Africa, Calderón of Mexico and many others C were desperate for a positive outcome. Obama needed a strong deal perhaps more than anyone. The US had confirmed the offer of $100bn to developing countries for adaptation, put serious cuts on the table for the first time (17% below 2005 levels by 2020), and was obviously prepared to up its offer.<br />
<br />
　　那么，中国是怎么让其他国家就范的呢？首先，它采取了一个极端强硬的立场：中国不需要任何协议。正像一个发展中国家的外交部部长对我说的：“雅典人对斯巴达没有任何馈赠”。另一方面，特别是西方国家领导人（当然也包括巴西，南非，墨西哥等国的总统）对于一个积极的结果过于企盼。奥巴马是他们中最需要谈判成功的人- 美国国会已经批准了对发展中国家的100亿美元的援助，在谈判桌上做出了首次巨大的让步（2020年照2005年减排百分之17），并且显然已经为这些提议做好了准备。<br />
<br />
　　Above all, Obama needed to be able to demonstrate to the Senate that he could deliver China in any global climate regulation framework, so conservative senators could not argue that US carbon cuts would further advantage Chinese industry. With midterm elections looming, Obama and his staff also knew that Copenhagen would be probably their only opportunity to go to climate change talks with a strong mandate. This further strengthened China's negotiating hand, as did the complete lack of civil society political pressure on either China or India. Campaign groups never blame developing countries for failure; this is an iron rule that is never broken. The Indians, in particular, have become past masters at co-opting the language of equity (&quot;equal rights to the atmosphere&quot;) in the service of planetary suicide C and leftish campaigners and commentators are hoist with their own petard.<br />
<br />
　　更重要的是，奥巴马迫切需要向参议院证明他能够将中国圈在任何一个气候框架之内以便使保守的参议员们相信美国的减排不会促进中国的工业增长。奥巴马和他的助手们意识到了中期选举的不乐观，他们也知道哥本哈根是使他们自己能够打出强有力的气候牌的唯一一次机会。中国在这一点上再一次占到了优势，因为中国完全不会受到来自国内的政治压力。舆论群体永远不会指责发展中国家的失败 C 这是一条铁打不动的定律。而中国已经超过印度，成为了最会利用这种公平言论（对于大气层的平等权利）毁灭地球的能手，这使左翼社会活动家和评论员搬起石头砸到了自己的脚。<br />
<br />
　　With the deal gutted, the heads of state session concluded with a final battle as the Chinese delegate insisted on removing the 1.5C target so beloved of the small island states and low-lying nations who have most to lose from rising seas. President Nasheed of the Maldives, supported by Brown, fought valiantly to save this crucial number. &quot;How can you ask my country to go extinct?&quot; demanded Nasheed. The Chinese delegate feigned great offence C and the number stayed, but surrounded by language which makes it all but meaningless. The deed was done.<br />
<br />
　　作为谈判的最后一击，中国代表坚持取消可能在海平面上升中丧失其国土的岛国以及低地国家强烈要求的1.5摄氏度目标。马尔代夫总统Nasheed在布朗的支持下，为这个目标与中国代表展开了强烈的争执，他质问道：“你们怎么能够要求一个国家走向灭亡呢？” 中国代表假装受到了很大的冒犯。这个数字被保住了，但是条款文本中的文字游戏确实它失去了实质性的意义。谈判就此结束。<br />
<br />
　　China's game<br />
<br />
　　中国的游戏<br />
<br />
　　All this raises the question: what is China's game? Why did China, in the words of a UK-based analyst who also spent hours in heads of state meetings, &quot;not only reject targets for itself, but also refuse to allow any other country to take on binding targets?&quot; The analyst, who has attended climate conferences for more than 15 years, concludes that China wants to weaken the climate regulation regime now &quot;in order to avoid the risk that it might be called on to be more ambitious in a few years' time&quot;.<br />
<br />
　　所有这些都引出一个疑问：中国想要什么？一个当时在场的英国的分析师问道：为什么中国“不仅否决自己的目标，同时还拒绝其他国家达成任何目标？” 这个参加过超过15年气候会议的分析师的结论是中国弱化气候公约以便“保证其不会在最近几年之内承担被要求减缓发展速度的风险”。<br />
<br />
　　This does not mean China is not serious about global warming. It is strong in both the wind and solar industries. But China's growth, and growing global political and economic dominance, is based largely on cheap coal. China knows it is becoming an uncontested superpower; indeed its newfound muscular confidence was on striking display in Copenhagen. Its coal-based economy doubles every decade, and its power increases commensurately. Its leadership will not alter this magic formula unless they absolutely have to.<br />
<br />
　　这并不代表中国对于全球变暖问题不严肃。中国具有着强大的风力和太阳能发电工业。但是中国的增长，与日俱增的政治和经济地位大部分是建立在廉价的煤炭上的。中国了解自己正在成为一个不可比拟的超级力量；这与上哥本哈根会议上的强硬表现相映成趣。中国基于煤炭的经济每隔10年就会翻一番，它的实力正在以令人惊愕的速度增长。除非在万不得已的情况下，中国的领导人不会改变这样一个极具膨胀的发展路线。<br />
<br />
　　Copenhagen was much worse than just another bad deal, because it illustrated a profound shift in global geopolitics. This is fast becoming China's century, yet its leadership has displayed that multilateral environmental governance is not only not a priority, but is viewed as a hindrance to the new superpower's freedom of action. I left Copenhagen more despondent than I have felt in a long time. After all the hope and all the hype, the mobilisation of thousands, a wave of optimism crashed against the rock of global power politics, fell back, and drained away.<br />
<br />
　　哥本哈根的影响要远远超过一个糟糕的协议，因为它还展现了全球地缘政治上的巨大变迁。这个世纪属于快速发展的中国，但是中国的领导人却不仅没有将多边环境治理提升到议事日程上，反而将其视为其自由发展的障碍。我以无比沮丧的心情离开哥本哈根。人们的希望，声势浩大的宣传，千百万人的动员和乐观主义情绪全部被强权政治的重拳击得粉碎，烟消云散了。<br />
<br />
=====================================================<br />
关注全球变暖不如关注中国变脏。<br />
