“共产党能得八十分”，评分标准是什么。<br />
<br />
“假如让我们来执政，我们能得三十分么” 首先，应当如何执政（依法、民主、透明、开放。。。）；其次，“我们”又是什么？执政党是一个政党，“我们”又不是政党，根本就无法执政，又何谈执政的成绩；第三，在中国这种政治现实中，这个问题无法回答，因为共产党的权力体系渗透到社会的每个角落，而且它拒绝任何制约它、监督它的力量，即使不存在上一点所讲的主体适格问题，你们也根本没机会执政。谈论人在1000度高温里的体感根本没有意义，因为那么高的温度根本不容你存在1秒钟。<br />
<br />
第三，批评共产党，就是要取而代之吗？他们批评的目的是什么。共产党执政的目的是什么。二者是否一致。共产党自身成为这种目的的阻力时，比如现在，取而代之是顺理成章的。厨师的职责是做菜，但一位大厨做菜难吃，又拒绝改正，换掉他是很自然的道理吧。但依据上一条，这种观点在党的势力的包围之下，你是说不出来的。<br />
