============<br />
以下叙述是建立在你想跟这种人讲道理的基础之上。但说实话，直接骂他或者无视更省事。这样的人要是不能留在学校或者考进机关，多半就没前途了。但我不大相信他毕业就失业。能说出这种话的人，总觉得不是省油的灯。<br />
