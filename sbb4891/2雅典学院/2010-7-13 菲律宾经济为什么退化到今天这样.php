&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp;&nbsp; &nbsp;同时代的菲律宾，最好的医疗技术水平（至今，马尼拉大学的医学院的个别专业，在亚洲还是数得上的）。<br />
&nbsp; &nbsp;&nbsp; &nbsp;拥有亚洲最好的金融业，所以亚洲开发银行的总部设在了马尼拉（据说风传要迁往上海）很多世界级的企业的亚太总部不在香港，更不在新加坡，他们设在了马尼拉。看看香港的老电影，台词里，经常有菲律宾财团的说法，菲律宾的发达可见一斑。<br />
