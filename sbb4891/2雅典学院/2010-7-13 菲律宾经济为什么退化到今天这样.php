日本在战前的弊病包括：<br />
第一、军阀。这个人人都知道，我就不展开说了。<br />
第二、天皇有不确定的权力。当时日本法律貌似给天皇规定了等于独裁者的权力，但实际上天皇显然又没有那么大的权力，习惯上天皇要听重臣的，但重臣怎么来的又没有明确的法律规定。总之，天皇的权力是不确定的，在实际操作中也是如此。这是一个国家很大的隐患。<br />
第三、土地问题。日本有长子继承制，战前工业化和城市化又不彻底，导致农村土地问题严重。<br />
第四、财阀。<br />
可以说，美国人以武力为后盾，强行解决了前三条，最后一条其实也打算解决，但是为了防止损害经济，没有解决。但不管怎么说，美国人的动作也够彻底了。<br />
如果这些政策套用于西班牙殖民地，应该包括：<br />
第一、消灭军队（改编为自卫队），用军事占领和军事条约，彻底消灭军事独裁的可能性。<br />
第二、进行反天主教运动，没收教会财产。（美国人自己就反天主教，当然应该这么干）<br />
第三、土改。<br />
第四、当然，还得枪毙一批有头有脸的人。<br />
如果做到了上述四条，西班牙殖民地的传统根基也就被彻底动摇了。但是，美国人怎么可能对西班牙殖民地（比如菲律宾）下这么大的狠手？其结果不好，也就可想而知了。</div>

							
							
							
							
