欧巴马履行竞选时从伊拉克撤军的承诺，七年前出征伊拉克的美国人回家了。<br />
<br />
因为当年布殊打萨达姆时，中国政府极力反对，在安理会给美国拆台与设置障碍，在舆论方面调动一切媒体来声援他们的萨达姆“同志”。为萨达姆独裁政权撑腰助威。并声言：伊拉克萨达姆政府的主权被侵犯，主权高于人权。<br />
<br />
当时，中国媒体每天宣传两个人的声音，一位是萨达姆的新闻部长萨哈夫，用他浪漫主义的臆想代替新闻，用谎言代替事实。另一位是CCTV的军事评论家张召忠，他以毛氏的人民战争去形容萨达姆的共和国卫队，以他学来的中国伪军史去展望萨达姆的军队。<br />
<br />
这七年，在中国媒体上看不到伊拉克修复战争创伤，清除萨达姆罪恶的信息，而且天天发送暴力恐怖新闻，或是美军还击袭击他们的恐怖分子，却伤了多少平民毁了多少民房。总之，没有萨达姆同志的伊拉克，就是一团糟。此时，在新华社分管了多年国际新闻的李慎之先生已在医院，仍关心中东战局，要老友曾彦修（杂文家严秀）每天打电话到病房，转告战况。后来曾彦修说，我必须告诉泉下的慎之，大规模杀人武器没找到，却发现萨达姆五处宫殿，十九处万人坑，有的人挖出来，身上库尔德民族衣服仍清晰可辨。<br />
<br />
我听了，不禁想到中共之所以这么反覆强调主权高于人权，不正是与萨达姆同志心心相印吗？<br />
<br />
那时，巴格达市民的愤怒表现在拉倒萨达姆的铜像，这画面向海外播放的电视台一闪即过，国内电视台却禁止出现。怎么对这独夫民贼就这么一往情深，真这么兔死狐悲么？几年后，重庆塑起三十米高的毛像，湘潭更大规模造毛神礼拜，才明白是不愿由萨达姆倒下做前鉴，就像斯大林的尸体弄出红场烧了，只是塑任何偶像在地上，都必倒！几千年帝王还塑得少吗？甚至墓中殉葬物也被盗。只有塑在人民心中的碑，最经风雨与历史的考验。<br />
<br />
当年美国之出兵伊拉克，按中共的说法是：美国贪图伊拉克的石油而已，何况总统布殊有德克萨斯石油财团背景...今天用事实来检验，却是美国人付出近五千生命，换来伊拉克一个民选的民主政府。而石油却被中国人拿走了。这事令人联想到二战期间，中国人被日本从东南亚封锁断了输血管道，又是美国人以两千多年轻生命的牺牲，开辟了从印度飞云南的驼峰航道，协助中国战胜日本。而今天飞机从这航线经过，喜玛拉雅山谷几千公里航道上还可见死难美军白骨与两千架飞机残片铺出的白色航线。中国人把美国当宿敌来反对，道义何在？<br />
<br />
今年三月，不是胜利的美军出场而是伊拉克民主政府出面向全世界石油招标，中国、美国、英国、日本等平等投标。结果，中国海洋石油总公司与中石化获得伊南部米桑三油田二十五亿桶的开采权，每桶只付二，三美元。又是一次美国人以生命代价给中国人换来胜利果实，我不禁对今天还在诋毁咒骂美帝的那些人发出质问：你们的良知良心真被中共洗脑换成狼性魔心了吗？欧巴马的伊拉克撤军与民主政府成长给中共一个结论：伊斯兰世界中，民主这普世价值，土耳其适用，伊拉克也适用。你们独裁的萨达姆同志被扫进历史拉圾堆，已是盖棺定论了。你们还在独裁这条黑道上闯，会比萨达姆下场好吗？</div>

							
							
							
							
