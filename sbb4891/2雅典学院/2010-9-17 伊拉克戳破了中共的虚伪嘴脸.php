																											<div id="postmessage_834869" class="t_msgfont">我来介绍下本文作者曾伯炎老先生，今年快80了，曾受迫害的老右派，现在成都。从2007年起长期翻墙看冉匪和我的博客。<br />
<br />
更多详情可看我的一篇旧文，转贴如下：<br />
<br />
幸存者手记<br />
宋石男 @ 2008-12-26 15:29 阅读(1851) 评论(27) 推荐(32) 引用通告 分类: 纪事<br />
<br />
使一个民族或一个国家具有共同的过去，并在此基础上构建一种同一体，也许是历史学最古老和最经常的社会职能。这种职能至今仍未完全丧失。<br />
<br />
然而，总有一些过去，它们虽是民族或国家的共同经历，却被扭曲原貌，武断地植入历史教科书及意识形态，于是成为神话――譬如义和团；与此同时，也总有另一些过去，它们确是民族或国家的共同经历，却被粗鲁地遮掩，试图一笔勾销，于是成为禁忌――譬如反 右。<br />
<br />
有学者认为，反 右运动是中共建政后，一系列大规模群众政治运动的起源，它与后来的大跃进、大饥荒、文化大革命，以及西单民 主墙、四 五运动、8平方运动，有着一脉相承的关系。触一发而动全身，为捍卫自有政权之合法性，当局遂采取回避与强制遗忘的手段。<br />
<br />
就在去年初，反 右五十周年之际，真理部召集各省市分舵负责人，口头传达了17条指示，其中包括：不能出版有关反 右与文革历史的文章、回忆录和相关书籍。稍后，2007年3月，中共中央办公厅下发《关于1957年反 右运动问题的若干意见》，再次重申固有立场。<br />
<br />
然而，如夏多布里昂所言，“当一切都在强权面前颤抖时，历史学者便出现在舞台上，接受公众的复仇委托”。在55万右派份子（官方数字，有研究者认为真实数字当过百万）的幸存者中（如今估计已不到十万），就有不少老人拿起笔，用记录个人史、群体史的方式，向昨日之劫难复仇。他们大多不是著名知识分子，但普通人的身份令他们的记载更加珍贵。毕竟，著名知识分子的遭遇常会赢得较多关注，相关史料也较易存留；而普通知识分子的遭遇则常会湮没在断碣残碑中，在汗青上留下一个又一个虫洞。【冉云飞兄也长期致力搜罗、编存相关史料，其不断增补的《右派资料编年知见录》，目前已初具规模（点此进入）】<br />
<br />
12月中旬，通过冉匪的邀约，我见到了十多位右派老人。他们当中最小的都70多了，最大的则有90多。而他们在劳改农场或监狱里渡过的日子，加一起超过200年。<br />
<br />
尽管历艰间关，这群老人的精气神却相当饱满，有个78岁的老人还能矫捷地翻越栏杆进入茶舍，而不少80多岁的老人则慷慨激昂地讨论各种时政问题。他们的见解如何先不论，这种“暮年不忘忧国”的精神，足以让人动容。我总觉得，知识分子首先是一种性格，其次才是他所拥有的知识技术。而在性格当中，首先是永远保持批判立场及精神。而在这群右派老人身上，我看到了知识分子最可宝贵的性格和精神。<br />
<br />
老人中有位叫曾伯炎的先生，76岁了，竟然经常上牛博网，还不时跟帖呢。他是四川日报的老编辑，1958年初被打成“极右”，累计劳改21年（先是劳教3 年，后在当地“就业”18年。而当年右派“就业”，实际上是继续劳改，因此又叫“二劳改”或“换劳改”），1978年始得“改正”，回到成都原籍。<br />
<br />
曾老先生写了本《幸存者手记》，记录了自己被打成右派后20多年的遭遇，以及在劳教农场亲睹的“右派受难记”，自费印刷成册。老人送了我一本，回家后细读，顿时跌落到那段荒诞、残忍的历史中。<br />
<br />
1958年1月30日，编辑部大会宣布曾伯炎为“极右”，随后由同事押解去报社附近天涯石西街的收容所，不料右派已爆满，只好改去新南门外新村四号，旋解乐山峨边沙坪劳改农场。<br />
<br />
在劳改农场，他先遭受了“睡刑”，开荒的上百人挤在一个帐篷里，挤着睡都不行，只能叠着睡。不过这种状况很快得到“改善”，因为大饥荒来临，劳教者不断饿死，每天人都在少，床位越来越宽，最开始人均不到1平米的睡觉地儿，后来人均4平米都有。曾老先生说，每晚都要瞅瞅周围的“战友”，明早醒来时好记住谁又不在了。<br />
<br />
但那些在劳改中饿死的右派们，没有人能全部记住。甚至有家属在他们去世20多年后，才知道他们已经不在人世，而且尸骨难寻。在乐山沙湾一农场后的荒山，有座孤坟被人们称作“万姓坟”。每当有家属来找死者的葬所，就被拉到那里去搪塞，不论姓赵姓孙，姓周姓王――“他就埋在里面”。<br />
<br />
右派的劳动强度相当大。曾伯炎在书中描述的苦役人在山道中运木的场景令人鼻酸。当地彝人运木，尚能用牛，苦役运木，则只能靠自己的肉身。曾老先生写自己运木的艰辛，“我用牛鼻绳拖一截百多斤重的木头，挣扎得绳子在肩头磨破衣服，勒进肌肉，有时甚至扑下身子，嘴唇快接触到路上的泥水，木头却如蚯蚓般缓缓蠕动，不肯疾行”。他还写另一个成都地质学院的右派学生刘西通，个儿高，力气大，一趟运300多斤的木头（好多换点饭吃），两根木头架在双肩，一条木头用绳子拴在腰间，从黎明运到天黑。曾老先生说：“当我看见十字架样的木头架在这汉子肩上，他脸上的肌肉因用力而挣得痉挛，便联想到教堂中耶稣受难的面容。如果后世雕塑家要为右派知识分子塑像，这是最好的素材”。那位1米8的大块头小伙子，刘西通，在大饥荒中饿死了，死于扛木途中。<br />
<br />
曾老先生还写了不少同营右派。<br />
<br />
比如作家刘盛亚，19岁留学德国，25岁受聘四川大学教授，专长为戏剧，与北方吴祖光齐名。他40岁时被打成右派（其父刘伯量，中国农学先驱，也被打成右派，可谓一门双俊彦，父子两右派），数年后即因病饿死于峨边沙坪农场。至今不知尸葬何处。<br />
<br />
又如儿科医师胡薇薇，因在饥荒年间写日记，说所谓瘟疫其实是营养不良引发的诸种疾病，被人揭发，也打成右派，送往峨边劳教。她侥幸活了下来，等到了平反。晚年信佛，青灯黄卷，聊度余生。当1950年解放军进西康时，胡只是10来岁的小姑娘，还在康定捧着鲜花送解放军，10年后，她就被解放军押进监狱。曾老先生忍不住问：“是小姑娘出了什么毛病？还是我们的时代患上了恶疾？”<br />
<br />
再如“后补右派”、中学生周永严，16岁即被送往峨边劳教。他得罪是因为13岁那年，在墙上抄了一首前人的古诗，中间有“长铗兴歌一再弹，风潮满目不堪看”，“只有虫声伴长夜，却无人语劝加餐”等句子，被一个家访的教育局干部撞见，作为证据送报当局。3年后，有人旧事重提，周永严遂遭清洗。他虽活到70 年代末的平反，但花季即入劳改农场，落下一身顽疾。80年代在文坛初露锋芒，90年代后即长期困卧病榻，2003年去世，年仅55岁。<br />
<br />
中学生被打成“右派”或“准右派”，并不罕见。在成都另一个老人王建军（大伙儿一般都叫他王大爷，在天涯曾注册ID ，就叫“王大爷”）所编《五八劫》一书中，就汇编了“1958年中学生社会主义教育运动”的丰富材料。这场运动由时任中共西南局第一书记、中共四川省委第一书记的李井泉发起，在四川省各中学及中专中开展，实质是以中学生为主体的“反右”运动。最终，被定为三、四类分子的学生，分到各厅局变相劳改的就有 3200人，这个数字还不包括已送劳教和押送农村监督劳动改造的。而当时全川的应届高中毕业生不到一万人。【王建军老人一直想将自己的书传到网上，希望有精通电子书制作、愿意协助的朋友给我来信<a href="mailto:ssnly100@sina.com">ssnly100@sina.com</a>，我把老人的联系方式给您。】<br />
<br />
我手中还有一本自贡王大可老师的《往事随笔》，详细记录了她的先生在1957年被打成右派及她在1970年“一打三反”中被定为“反革命份子”的遭遇。令我敬佩的是，王大可老师尽管大半生不幸，5岁丧父，12岁丧母，52岁丧夫，而夫妇两人又累遭政治歧视乃至迫害，却始终保持一颗达观的心。在这本书里，她冷静、从容的叙述颇见功力，而且没有半点戾气。她还在扉页上写了一段话给我：“这里部分记述了我们那一代知识分子的精神生活和政治际遇，希望能得到你们的 ‘了解之同情’”。“了解之同情”，语出陈寅恪为冯友兰《中国哲学史》所作审查报告，就我们年轻一代而言，对老一辈知识分子所经历的苦难，必须具备“了解之同情”，才能看到接近完整、接近真实的历史图像，如菲奇诺所言：“本身注定要消逝的东西在历史中获得不朽；不存在的东西变成存在；陈旧的东西焕发青春”。<br />
<br />
勒华拉杜里在其微观史学名著《蒙塔尤》扉页上，引用了印度《奥义书》的一段话：“通过一团泥便可以了解所有泥制品，其变化只是名称而已，只有人们所称的‘泥’是真实的……”<br />
<br />
关于右派的各种定义、诠释在不断变化，但反右运动中被打成右派的群体之遭遇及其映射的政治体制是真实的。阅读并传播那些幸存右派的记录，是我们认知、介入反右这段历史的最好手段之一。而依靠更多当事者、关注者的努力，最终必能重构一段反右信史。所谓“国可亡，而史不可灭”、“不信青山唤不回，不信青史尽成灰”。</div>

							
							
							
							
