													<h2>进化论在中国备受推崇的背后</h2>
						
						
																											<div id="postmessage_363971" class="t_msgfont">张撞鹿 发表于 2009-02-25 11:10（本文已发表于中国青年报冰点周刊）<br />
<br />
查尔斯・达尔文未必能料到，他的进化论会在中国受到如此厚爱。<br />
<br />
2003年，一份公众科学素养调查显示了进化论在中国有多么流行：71.8％的中国公众认可“人类是从早期动物进化而来”这个结论。<br />
<br />
调查的主持者、中国科学院教授李大光，至今还对这个数字感到惊讶。要知道，即使是在达尔文的故乡英国，情况也远非如此。直到几天前，“半数英国人不信进化论”的新闻，还频繁出现在报纸上。<br />
<br />
李大光的床头，摆着刚出版不久、美国学者浦嘉珉写的《中国与达尔文》。这本书详细探究了进化论进入中国以后的传播脉络。尽管很忙，李大光还是会经常抽时间翻上几页。他觉得，这本书切中了他平日的思考。<br />
<br />
当达尔文诞辰200周年到来之时，许多人开始对进化论在中国的流行进行思考。美国芝加哥大学进化学系教授龙漫远认为，从严复到毛泽东，进化论在中国地位的确立，有一条清晰的主线。<br />
<br />
进化论是以一种“谦卑”姿态进入中国的<br />
<br />
最初，进化论是以一种“谦卑”姿态进入中国的。1897年4月12日，一份报纸在脚注里介绍了严复和他的译作《天演论》，并没有引起太大的反响。<br />
<br />
此时，达尔文的《物种起源》已发表近40年，进化论终于作为一个西方学术观点，进入中国民众视野。100多年后,龙漫远仍觉得，如果他是严复，也会选择翻译《天演论》。<br />
<br />
2月23日，他坐在芝加哥一家咖啡厅里，回溯19世纪末的中国历史。正当战争屡屡失败、改革无功而返之际，1894年，甲午战败，曾长期在海军任职的学者严复“想唤起改变贫穷落后的觉悟”，于是第二年便提笔翻译《天演论》。<br />
<br />
为什么选择《天演论》？龙漫远认为，原作者赫胥黎在书里，意在用《物种起源》里面的一般规律、用“进化”这个科学观点解释人的事情，解释社会问题。这种功能，在《物种起源》里是找不到的。<br />
<br />
“严复并不是学生物的，他翻译的目的在于向人们传达对社会的理解。”龙漫远说。为了阐述自己的观点，严复干脆在书中做了大量批注。<br />
<br />
在这些批注中，严复对社会达尔文主义努力鼓吹。“物竞天择、适者生存”成为这位翻译家留给后世最著名的格言。<br />
<br />
“历史在这里玩了个吊诡。”就读于美国哥伦比亚大学政治学系的博士生林介绍，在原著中，作者赫胥黎对社会达尔文主义持批判态度，但译作却变成了鼓吹社会达尔文主义的经典文本。<br />
<br />
“一方面，救亡图存确是一时之亟，另一方面就世界范围而论，社会达尔文主义伦理学也风头正健，赫胥黎的伦理学正被越来越多人看作过了时的、温情脉脉的理想主义空谈。”林解释说。<br />
<br />
后世的反思，并不能阻止《天演论》在1898年出版后一纸风行。当年刚满17岁的中学生鲁迅，特意花了500文钱买回这本书，觉得很新鲜，一口气读了下去。同样受到进化论思想影响的，还有青年时代的毛泽东。<br />
<br />
在李大光看来，“这是近代科学文化的第一个高潮”。进化论成为街头巷尾人们谈论最多的话题之一，也是中学作文最常见的命题。而康有为、梁启超等知识界人士对于进化论精神在社会问题上的应用，更是乐此不疲。一场“保种”的运动，随即浩浩荡荡地开始了。<br />
<br />
《天演论》风头太盛，以至于20年后，当《物种起源》终于被翻译到中国时，影响力反而大不如前者。这本书为中国留下许多遗产。经由维新变法、新文化运动等社会事件，社会达尔文主义的灵魂一直随着中国知识分子救亡图存的努力而流传。浦嘉珉在《中国与达尔文》一书中评判说：进化观念在中国的传播，是“在政治学语境中发生的”，而基本上被从科学或博物学的传统中剥离出来。<br />
<br />
与此同时，在国外，另一场对达尔文和进化论的演绎正在发生，并且等待时机进入中国，准备接过进化论在中国传播的大旗。<br />
<br />
“没有争论，也不能有争论”<br />
<br />
小学的时候，龙漫远在父亲单位的藏书里，发现过一本与进化论有关的书，是翻译版，“文字写得真美”。他立刻就被吸引了。<br />
<br />
不过，真正接触进化论，要等到读中学时。那是上世纪70年代，进化论从两个渠道进入他的生活，一个是在生物课堂上，另一个是在学习马克思主义的时候。龙漫远还记得，读恩格斯《自然辩证法》时，也在里面看到过进化论。<br />
<br />
“马克思、恩格斯和毛泽东都强调达尔文的进化论，并且有的时候把进化论的观点演绎到社会问题。”龙漫远回忆。从马克思主义传入中国那天起，进化论在中国的命运，就和这个新兴的学说发生了关联。<br />
<br />
这种关联很早就开始了。1883年，恩格斯在马克思墓前发表讲话时，曾经为他留下了著名的论断：“正如达尔文发现了有机自然界的发展规律，马克思也发现了人类历史的发展规律。”<br />
<br />
关于马克思和达尔文之间的“惺惺相惜”，人们不难在报端上找到若干证据。其中一个例子，是1931年由苏联《在马克思主义旗帜下》杂志上发布的。该杂志声称发现了达尔文给马克思的一封信，足以证明马克思曾想把《资本论》第二卷题献给达尔文，可见马克思对达尔文思想之重视。<br />
<br />
这个说法一直被用来证明马克思的学说与达尔文进化论的密切关系，流传甚广。然而，许多人并不知道，早在1979年，美国明尼苏达大学政治学系副教授泰伦斯・波尔已经发表文章，论证该信并非写给马克思的，而马克思也从来没有过要把著作题献给达尔文的念头。<br />
<br />
事实上，马克思本人未必真的看重进化论，当一些社会主义者试图将自然选择概念引入社会主义学说中时，马克思还对他们大加嘲讽和批评。<br />
<br />
不过，林认为，在马克思之后，人们有意无意地强化了他与达尔文之间的联系。许多后来的社会主义者推重并借鉴自然选择理论，对宣传达尔文主义也“不遗余力”，这是因为，在唯物主义者看来，进化论是他们“反对有神论的天然盟友”。<br />
<br />
龙漫远的解释则是，在社会主义者眼里，社会发展的方向是“进步”的。这和进化论者表达的“从低级到高级、从简单到复杂”的观念是一致的。<br />
<br />
这种氛围一定程度上延续到了中国。从一开始，进化论在新中国就获得了崇高的地位。龙漫远回忆，当时的进化论是以“官方”、“正宗”的面目出现的，他理所当然也认为，“没有争论，也不能有争论”。<br />
<br />
直到改革开放前后，“这一代人”才开始反思。龙漫远读到了一套《走向未来丛书》，介绍了西方的很多东西。从那时起，他才真正开始思考和比较，并第一次发现对于世界的解释，除了进化论，还有宗教。<br />
<br />
中国教科书里的进化论是完美无缺的<br />
<br />
在美国，龙漫远曾经被叫上法庭，为一起家长起诉学校教授神创论的官司作证。作为科学家证人，他在庭上对法官和陪审团讲解了基因是如何起源的。<br />
<br />
他觉得，因为存在争论，许多美国人从小就会仔细了解进化论讲的是什么。而在中国，进化论被作为一个事实来讲授，大家都知道人是由猿进化而来，但“好多人其实理解得不深刻”。<br />
<br />
“中国教科书里的进化论是完美无缺的，学生不需要对其进行批判性的反思，只要接受即可。”林这样反思自己在中学时学过的进化论。<br />
<br />
2月12日，一场小型的讨论在某个网站上进行，几名30岁上下的年轻人，回忆起自己在中学课本里学到的进化论。有人感叹：当时的教育十分有效，自己“从来不曾怀疑过进化论”。<br />
<br />
进化论进入中国的过程中，始终没有引起太大争论，李大光觉得这“很要命”。<br />
<br />
“争论并不是胡乱否定，而是进行科学的探讨。”李大光认为，“这种讨论本身比进化论更有价值”，而没有经过讨论的进化论，“并不能作为灵魂进入我们的文化并固化下来，只停留在一种外来理论的层面”。<br />
<br />
“在西方，进化论是在讨论的情况下被接受的，而中国人则是不假思索地接受。”李大光据此认为，尽管中国人认可进化论观点的比例更高，但这并不能说明进化论在中国更加成功。<br />
<br />
林也发现，在美国课堂上，教师在讲授进化论的同时，会告诉学生进化论在哪些环节上证据尚显不足，对哪些问题的解释力较弱，哪些方面易于遭受攻击，这些攻击对理论整体的破坏力如何，等等。<br />
<br />
“由于没有接触过反面的观点与论证”，中国学生对进化论这一信条的辩护能力，也就相应减弱了。<br />
<br />
李大光观察到，许多西方理论在中国有不同的命运。比如，与进化论备受拥戴相反，“人口论”在中国遭到了猛烈的批判。而在这截然相反的待遇背后，他看到了“相同的逻辑”。问题的关键在于，中国公众通常并没有判断，而是“由政府和科学家判断对错”。当政治运动一来时，他们也更容易被利用，对某种观点盲目地“批判和打倒”。<br />
<br />
对于政治和科学的关系，龙漫远看到了有益的一面。尽管科学有时候为政治服务，但他觉得，这不应该由科学负责。相反，一定程度上，“马克思、毛泽东对进化的肯定，起到了积极作用”，因为无论过程如何，从结果上来看，传播了进化论，能“帮助人们对自然界作出理性判断”。<br />
<br />
尽管他也承认，国内对进化论的认识“不够准确，连翻译都没有翻准”。<br />
<br />
“进化”学系的教授<br />
<br />
在博士毕业论文答辩的时候，龙漫远的主考官是个虔诚的基督徒。他担心自己的答辩无法通过，于是试探地告诉教授，自己的演讲将涉及进化论。教授一眼就看出他的顾虑，说：“年轻人，我从星期一到星期五信进化论，星期六和星期天信上帝。”<br />
<br />
他逐渐发现，经过最初的激烈冲突，科学、宗教在西方国家的许多人心中已经可以互相包容，并不冲突。甚至连罗马教皇本人也宣布相信进化论。在达尔文诞辰200周年到来的时候，英国主教专程来到达尔文墓，献上一大捧花。<br />
<br />
不过，龙漫远发现，在国内，人们通常还是把宗教和科学看作对立的两面，看成敌对的。“我们一直被这么告知。”他说。可那已是几十年前的旧观点了。<br />
<br />
许多误解还围绕在进化论的周围。龙漫远指出，甚至连“进化”这个约定俗成的称谓，也是一种误读。<br />
<br />
严复在翻译《天演论》的时候，用的是“演化”来表达达尔文“evolution”的概念，为了防止误解，他曾特意在注解里注明，这个词不仅有“进化”，也有“退化”的意思。龙漫远解释，这就是说，除了“从简单到复杂”，演化还有“从复杂变简单”、“变单一”的可能。<br />
<br />
不过，当后来北大教授马军武翻译《物种起源》时，他从日文版翻译成了“进化”，并从此沿袭到今天。学术界曾经对此有过讨论，很多人试图扭转这个翻译，但在公众中，这个误读并没有改变。<br />
<br />
甚至，他自己对别人介绍自己的身份时，也不得不告诉别人，自己是“进化”学系的教授。<br />
<br />
在种种误读中，中国和世界一起，迎来了达尔文的第200个生日。<br />
<br />
从2月12日开始，英国各地举行了许多大大小小的纪念性聚会。而在中国，人们并没有感到什么异常，甚至很难看出，这是个一直流行着进化论的国家。</div>

							
							
							
							
