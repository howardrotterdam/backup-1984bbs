																											<div id="postmessage_764586" class="t_msgfont">天机之二：中国地图暗藏“中国人汉字” <br />
中国34个省会城市（包括特别行政区）用笔巧妙地连起来，就会形成三个字“中国人”。&nbsp;&nbsp;<br />
“中”字刚好位于中国的西部地区，由西宁、兰州、西安、南宁、昆明、成都、重庆、贵阳、银川的9个省会城市组成。&nbsp;&nbsp;<br />
“国”字位于中国的东部和沿海地区，由北京、天津、上海、济南、石家庄、太原、呼和浩特、郑州、合肥、武汉、南京、南昌、杭州、沈阳、长春、哈尔滨、长沙、海口、福州、广州、香港、澳门、台北23个省会城市组成。&nbsp;&nbsp;<br />
“人”字位于中国西部地区，主要由乌鲁木齐、拉萨（昆明、西宁连线共用）两个省会城市组成。（摘自《扬子晚报》）<br />
<img src="http://fm411.img.xiaonei.com/blog/20080327/23/23/A138079614422CUC.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
天机之三：中国地图暗藏“五条龙”<br />
在中国地图上将全国34个省（自治区、市、特别行政区）的省会城市连在一起，形成5个繁体的“龙”字。中国古代有“五行之说”，这五条“龙”按五行排列。五个“龙”字，飘如浮云，宛如惊龙，省会城市都是龙脉必经之地。如没有香港、澳门特别行政区和台湾省，在地图上无论如何左画右画，都画不出完美的“龙”字。（摘自《澳门时报》《人民日报海外版》）<br />
<img src="http://fm381.img.xiaonei.com/blog/20080403/07/30/A903113782212OXC.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
中央“土龙”的龙字<br />
起笔于银川，经乌鲁木齐、西宁、兰州、成都、西安、重庆、拉萨等市，收笔于台北。土的五行方位在中央。土的颜色有黄色、棕色。“土龙”堂堂泱泱，宽宏大方，坚韧厚重，气宇轩昂。“土龙”起笔处及中央主要在中原，黄土高原，黄河流域，国土地貌颜色多是黄色，与五行之中央土的颜色类同。<br />
<img src="http://fm351.img.xiaonei.com/blog/20080403/07/31/A907612184546OXC.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
南方“火龙”的龙字<br />
起笔于南海，经台北、香港、澳门、广州、福州、杭州、南昌、长沙、上海等城市，收笔于西宁。火的五行方位在南方。火的颜色有红色、紫色。“火龙”如燃烧的烈焰，蓬勃兴旺，热情奔放。“火龙”左右起笔处的山川地貌颜色多为红色，与五行之南方火的颜色相似。<br />
<img src="http://fm411.img.xiaonei.com/blog/20080403/07/48/A013416964112FEN.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
北方“水龙”的龙字<br />
起笔于黑龙江省，经哈尔滨、长春、沈阳、北京、天津等市，收笔于海口。水的五行方位在北。水的颜色为黑色、蓝色。“水龙”如行云流水，自然顺畅。“水龙”左部主要在北方黄河流域；右部主要在南方长江流域。黄河、长江流域都是中华民族的发源地，黄河、长江源源不断，生生不息地滋润着中华大地，孕育和繁衍了代代中华儿女。“水龙”左部起笔处一带国土地貌为黑土地。右部起笔和收笔处在海洋，与五行之北方水的颜色为黑色、蓝色巧合<br />
<img src="http://fm391.img.xiaonei.com/blog/20080403/07/31/A909214553111OXC.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
东方“木龙”的龙字<br />
起笔于东海，经上海、杭州、南京、合肥、济南、太原、呼和浩特等市，收笔于西宁。木的五行方位在东 木的颜色为青色、绿色。“木龙”似参天大树，枝繁叶茂，扎根于莽莽昆仑。“木龙”左右起笔一带多青山绿水，与五行之东方木的的颜色巧相吻合。<br />
<img src="http://fm381.img.xiaonei.com/blog/20080403/07/31/A911063014011OXC.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
西方“金龙”的龙字<br />
起笔于西方昆仑山。经拉萨、昆明、成都、重庆等市，过燕山，北京收笔于大兴安岭。金的五行方位在西。金的颜色有白色、杏色和金色（金以白金为最贵）这正巧合了唐代人杨益分析北京自然山水条件所说：“燕山最高，象天市、盖北干之正结。其龙发昆仑之中脉，绵亘数千里。”“金龙”头顶昆仑，像金字塔型，怀柔显刚，挺立坚强，凸显中华民族的脊梁。“金龙”左右起笔处多冰天雪地，国土地貌颜色以白色为主，与五行之西方金的颜色一致。</div>

							
							
							
							
