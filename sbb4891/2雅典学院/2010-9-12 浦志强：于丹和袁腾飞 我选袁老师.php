													<h2>浦志强：于丹和袁腾飞 我选袁老师</h2>
						
						
																											<div id="postmessage_826193" class="t_msgfont">于丹和袁腾飞，都是教书先生，除了性别，不同之处还很多。两位都上过电视，网上都还有视频。于丹能卖书能卖碟，出门讲座能有保镖，袁腾飞没了舞台，坐家里头也快配保镖了。领导出门带保镖，是怕让人伤着；于丹演讲带保镖，是怕被人亲着了；韩寒袁腾飞也有保镖，那是有人怕他们被亲近着。<br />
<br />
&nbsp; &nbsp; 于丹的口才一流，擅长一气呵成一字不易；袁某人口水超一流，满嘴的片儿汤嗯艾着是。俩人亦庄亦谐，于丹更显端庄，可惜她只会端着装，袁腾飞还颇能谐，大珠小珠落玉盘。于丹习文，从来都是只说正确的，就算朔风把草吹得再低，她也不想让你看见牛羊；腾飞治史，句句单挑不爱听的说，没风都想让你看见黄鼠狼。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;我爱听袁腾飞，温故而知新；看不得端和装，有幸撞个正着，就只好换个频道。但自觉对于丹和袁腾飞，不愿褒贬，只想表明好恶而不涉及其他，老实说我忍很久了，勒住自己没说什么，至少没把俩人儿拉一块儿说。跟于丹有过同事经历，对她的才华和作人，一向印象不错有所期许，倒是知道袁腾飞这同行，是近来的事，我钟情于他的率真和口无遮拦，这就叫人以群分。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;说句真话能把人吓半死，就是当今的心态，袁腾飞倒霉，顶多算仲尼之厄，没啥了不起。但于丹迎合意识形态，周瑜打黄盖假戏真做，跟当局投怀送抱各取所需，赚的是庸众的喝彩，就透着三俗透着没劲了。夸个劣徒都敢说他是自己“生命中的一个传奇”，如此“妆诸葛之多智而近妖”，这叫矫情叫太不中国。口若悬河是本事，但人有才有品还要有节，假如逮着个话筒，就抽疯就条件反射，就没完没了，活脱脱上满了弦的发条，不伸直腿把自己放倒就罢不了休，全不顾聂卫平那家伙睡没睡着，那就太可乐了。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;电视上于丹普及的，不是传统文化，但她构成了传统文化。袁腾飞唠叨的，倒符合传统对史家的底线要求。师者，所以传道授业解惑，传道为上上之选，为人师表要不欺无名，不可以巧言令色。文化深入骨髓，既无师自通也难得普及，它就是你我它就是众生，它是什么它就是什么。于丹想必熟读《论语》，该知道“知之为知之，不知为不知，是知也”，但我看就这点她没做到，眼见着越发不知而强以为知，脑袋太大而脑子太小。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;当局造反起家，轻车熟路的是二把刀和二杆子，打倒孔家店是本行，可身在其中打也打不倒，骨子里就是中国，这才是传统文化。鼎革六十年了，马上治不了下了马还不习惯，所以老找不着执政的感觉，浑身透着股流氓匪气，转身至难，也就越发想找到一条通往孔子的路。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;于丹和何新的老婆，在这条路上帮闲搭了座桥，有没有人肯走，能不能收得到过桥费，那还得另说。袁腾飞能把这桥给拆掉，所以他才会暂时倒霉。<br />
<br />
