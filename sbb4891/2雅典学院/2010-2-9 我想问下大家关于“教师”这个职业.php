我想不管那种职业，主要要求就俩：一是职业道德，二是职业能力。<br />
先说说职业道德，很多人对教师道德要求比较高，几乎是道德君子，成仁成圣的标准，阿弥陀佛，这样的话，世界上没有几个人能当教师。<br />
我个人觉着吧，教师的道德要求应该是这样滴：日常生活中的略高于常人标准，不过你不可能指望在一个道德堕落的很厉害的社会里要求教育行业的人太高于常人标准，对不起，你要找的是牧师不是教师。就拿嫖赌来说，我所闻所见俺这个行业的从业人员是有嫖有赌，不过嫖少赌多，嫖少因为是很容易造成暴露。赌多是其他娱乐太少。职业生活中的道德是最起码的不歧视学生，不故意补课敛财等等。整学生敛财的，旁敲侧击要家长孝敬的。我也见过，比例还比较大。厚颜无耻的尤其令人讨厌，我也见过。但是你要说达到什么90%的比例那就实在太夸张。60%左右肯能比较可靠（我这个是凭直觉，很可能有错）。<br />
接着说说职业水准问题，我先举个例子，我是学文科的，大学我自修过高等数学。一年俩新来的数学老师，本科的。在做高三的简易微积分的时候，竟然在凑答案。我一看，是一个最基本的概念题。而且题目有错（具体题目记不清了，是江西省前几年（2005？）的高三数学练习册）他们竟然没有发觉。那列位知道大部分中学老师的起点了吧。很多人是边学边教。（不过话说回来，高中的教学基本上经验活，技术含量比较低（理科的奥赛除外））大部分人除了教科书基本上是不看其他书的，杂志报纸也主要是翻翻，还主要是什么环球呀，家庭医生之类的。普通教师一年下来给自己买书的支出（指自愿，非摊派，非公费）可能就是0元。其他的充电也是很少的。现在流行的教硕，俺们这块有些人报。不过就我所知，这种硕士是垃圾中的战斗机。<br />
所以，综合上面来看，韩寒说老师是社会的渣滓（大意），这句话几乎可以有60%以上是有保证的。<br />
题外话：关于补课之类的问题，这是很多学校的教师最主要的福利来源；而且在同等生源下，人家都补课你不补课的，高考会死的比较难看。而教育产业化教育部门的敛财，普通老师得不到什么实惠。大头的大头，都在上面的领导手中（我是从我所在学校的经验出发，有可能不符合很多其他地方的实际）。</div>

							
							
							
							
