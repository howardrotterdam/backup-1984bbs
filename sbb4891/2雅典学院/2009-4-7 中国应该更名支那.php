													<h2>“支那”的来源</h2>
						
						
																											<div id="postmessage_69906" class="t_msgfont">　　“支那”的来源ｈｔｔｐ：／／ｃｈｉｎａ。ｃａｌｓｕｎｓｈｉｎｅ。ｉｎｆｏ／ａｒｔｉｃｌｅｓ／２００４／０８／０１／０００５８６。ｐｈｐ长期困扰学界的一个难题是：世界相当多的国家，特别是古代西方对于中国的称名“支那（Ｃｈｉｎａ）”是从何而来的？它与西方接触中国的历史有什么关系？这是一个有重要现实意义的研究，因为历史并不是过去，在一定意义上正如西方史学家克罗齐所说，一切历史都是当代史。而国家名称则是其历史符号，它所指与能指都与这个国家的历史直接相关。直到近年，关于“支那”一次的争论仍然狠激烈，当有人使用“支那”来称呼中国人时，海内外华人都对于它是不是有贬低中国人的含义十分官关注，这方面的争论一直不断。<br />
　　因此辨明这一名称的来源时十分重要的。<br />
　　笔者认为，以往研究这一问题的主要是根据零星片断的史料，没有古代语言史上的相关资料证明，没有能从西方与中国交往历史过程来考虑，特别史脱离开东西方文化交流背景DD其中相当重要的是举世闻名的丝绸之路这一东西方文化交流的重要历史进程DD所以研究中往往吉光片羽、不成系统。<br />
　　在这种研究方式中，没有考虑道虽然同样是欧洲国家，但由于与丝绸贸易的关系不同，与中国交往的历史时代不同，所以对中国有不同的称名。也就是说，在中西称名的历史演变中，要同时考虑历史接触与语言系统等不同因素产生的影响。<br />
　　最明显的一个特点就是，欧洲重要语言系统中，斯拉夫语系与印欧语系对于中国的称名是完全不同的。俄国人称中国人为Китай“契丹”　（Ｋｈｉｔａｎ）。这是由于俄国人把中国人与其担任混在一起。中国人与契丹人在同属于黄色人中。契丹人较早与罗斯人发生交往，据《多桑蒙古史》记载，契丹是１０世纪之初行其余中国辽东之北的民族，先居住在鞑靼地方，然后占据了中国北方土地，公元１１２５年，北女真人所灭。虽然契丹人存在的时间并不太长，但这一段时间恰好是古代罗斯人建立起自己的公国的时代，根据俄国著名史诗《伊戈尔远征记》等记载，这一时期是古代基辅公国等斯拉夫国家兴起的时代。而中国与黑海地区的斯拉夫人的交往从古代起就受到北方游牧民族的阻碍，以后斯拉夫人东迁，来到伏尔加河流域与亚欧草原上，与中国直接交往相对更少。罗斯人最早接触的是契丹人，也就把中国人看作是契丹民族。在这种历史接触中，是斯拉夫语言使得中国民称固定为“契丹”的读音，而没有采用“支那”的读音。也就是说，由于历史原因，斯拉夫民族在中国与西方民族的丝绸贸易中距离较远，而没有以丝绸之国来称呼中国。<br />
　　称中国为Ｃｈｉｎａ的古代民族则是印度人与欧洲人，所以“支那”（Ｃｈｉｎａ）包括“印度支那”一类称呼实际上来自两个方向，一个是欧洲，一个是印度，学者们根据历史接触来研究，往往不能确定其来源方向之间的关系。但是，这两个语系在１９事迹之后北欧洲的比较语言学家发现是属于同一语系，这就为中国之名的历史索解提供了关键的条件。无论是古代印度还是希腊，都处于中国古代丝绸之路上，因此考察中国称名这一问题宜从中国与西方丝绸贸易这一总体思维来研究。作为一种有影响的大国的历史称名不是某个个别国家民族所能决定的，它只是一种历史过程的产物。这是我们考察的主要出发点。　ｈｔｔｐ：／／ｗｗｗ。ｉｎｔｌｓｈｏｐ。ｃｏｍ目前关于中国“支那”读音的称名中，有一下主要的看法。<br />
　　其一，中国秦朝说。认为中国称名Ｃｈｉｎａ来自“秦”的发音，这是一种流行广、拥护者多的说法。发过学着鲍狄埃（Ｍ・Ｐａｕｔｈｉｅｒ）等人提出此种见解。他认为支那称名起于梵语，而梵语中支那是因为中国古代秦朝而得名，所以称中国为“秦”<br />
　　（Ｓｉｎ、Ｃｈｉｎ），而Ｃｈｉｎａ后的ａ是葡萄牙人加上的。这种说法后来得到了发国汉学家伯希和的支持，伯希和认为：<br />
　　一方面是西方世界用Ｓｉｎｏｅ来指中国，用“塞里斯”来（此处可能烧了一个　“指”<br />
　　字DD笔者注）丝（ｓｅｒｍ这是用以指“丝”的一个中文词的古代形式），用Ｔｏｂｇａｔｃｈ（桃花石）拓跋氏；另一方面是中国用“拂林”来指罗马，用“犁干”来指亚历山大城（Ａｌｅｘａｎｄｒｉｅ），用“安都”来指安条克（Ａｎｔｉｏｃｈｅ），用“汜复”来指比凯『Ｂａｍｂｙｋｅ＊，不是幼发拉底河上的赫埃罗波利斯（Ｈｉｅｒｏｐｏｌｉｓ＊）』，用“罐潜”来指花刺子模（Ｋｌｗａｒｉｓｍ＊）。<br />
　　（【法】伯希和等著，耿N译：《伯希和西域探险记》，云南人民出版社，２００１年版。伯希和的《支那名称指起源》发表于《通报》１９１２年）按伯希和的看法，西方的中国称名中，中国与“塞里斯”是分开的。中国从“秦”<br />
　　的音译，而“塞里斯”指丝绸。由于伯希和是西方声名显赫的汉学家，所以在他之后，“支那”之名起于秦，以成为相当有影响的一种学说。中国著名的中外交通史家张星良先生也支持这种看法，这一说法在当代中国学术界影响甚大。<br />
　　对于这种观点也有不赞同者，如德国学者赫曼・雅各比（Ｈｅｒｍａｎ・Ｊａｃｏｂｉ）的反驳就十分有力，他指出这样的历史事实：早在公元前３００年，也就是印度丹陀罗笈多王朝时期的历史学家考铁利亚（Ｋａｕｔｉｌｉｙａ）曾经写过一本《政治论》，其中就使用了“支那”一词，记载了支那的丝绸贩运道印度的历史。而秦朝始建于公园前２４７年，也就是说，在此之前，印度已经使用了“支那”来称呼中国（考铁利亚，即考塔里亚，亦称考提利亚、者那迦，丹陀罗笈多・　毛里亚的宰相。曾著《政治论》一书，此书现在被考证为公元前２～３事迹所著。雅各比的论点发表于其论文《从考铁利亚论著中所见的文化及语言学资料》，载《普鲁士科学院学术报告集刊》第４４期，１９１１年）。<br />
　　第二种看法是“支那”为越南“日南”音译。“日南”（Ｊｉｈ－ｎａｎ）使越南的一个郡，在汉唐时期是通向中国的海上交通重镇，所属来中国的船只都在此处停留。德国学着利克托分（Ｖｏｎ　Ｒｉｃｈｔｈｏｆｅｎ）提出这种说法，并且得到了西方汉学界不少学者的赞同。法国学着拉克伯尔（Ｌａｃｏｕｐｅｒｉｅ）对这种说法提出反驳，认为这种说法有不少漏洞。其一，汉代日南郡并不像利克托分所说是在越南东京，而是南部。其二，汉代日难的读音不是Ｊｉｈ－ｎａｎ，其读音是ｎｉｔ－ｎａｍ，在广东读音中是ｙａｔ－ｎａｍ，这就与日难的读音完全不同，所以不可能是日南。<br />
　　第三种其实是一种古老说法，由古代希腊人提出，即支那之名可能来源于“赛里斯”国，而“赛里斯”国则与丝绸有关。但这种说法语焉不详，本身缺乏明晰论证，在伯希和之后就逐渐被人抛弃。<br />
　　此外还有其他一些看法，我们不一一详述。<br />
　　笔者认为，中国古代称名“支那”就是古希腊人所说的“赛里斯”，但是其形成“支那”一词，却另有一种语言传播的原因。由于中国与希腊之间没有直接商贸关系，经过梵语、叙利亚语、东伊朗语等古代语言的转译，形成了中国称名的不同读音。这一称名起源于中国的“丝”，语辞的意义就是“丝国”。比较文明学与世界文化交流史的新发现可以支持我们的看法。兹简单说明如下：<br />
　　１、从时代来说，中国丝绸远在公元前６世纪之前就传到印度，随后道中东与希腊罗马，随之出现产地中国称名，这是世界贸易史上的必然过程。这在秦建国之前，而且与越南日南无关。日南只是罗马人来华所经过的港口，必然在此之前已经知道中国，才有罗马皇帝遣使来华之举。而且，日难这样的小地方不可能成为中国丝绸这样举世闻名产品的产地代表，这可以说是历史的常识了。简单说，就是西方先知　“支那”而不会因日南而知中国，更不会把日南附会为中国。<br />
　　２、最直接的证据是印度经典与文学中中国的梵文称名Ｃｉｎａ，梵文经典《摩呵婆罗多》（Ｍａｈａｂｈａｒａｔａ）中已经提到了Ｃｉｎａ　（Ｍａｈａｂｈａｒａｔａ，Ｓａｂｈａｐａｒｖａｎ，９，２６，ｅｄ。ｂｙ　Ｐ。Ｅｄｇｅｒｔｏｎ。Ｐｏｏｎａ，１９４３￣１９４４）。《摩呵婆罗多》的成书年代为公元前４世纪，上文所提到的考铁利亚《政治论》的成书年代在公元前　３世纪，都远在秦国或者秦朝之前，而且，这一称名不会是从西方传入印度的，因为《摩呵婆罗多》中同时提到，与中国人相关的阿另一古代民族是基拉塔斯（Ｋｉｒａｔａｓ），这一民族应当是最早与印度产生交往的喜马拉雅山另一侧的民族。所以有的学者认为：“通过把基拉塔斯人和中国人联系在一起的情况来判断，古代印度人最早是直接通过东方路线来接触中国的。他们把中国人看成与基拉塔斯人一样的东方人。”（）Ａｓｔｈａｎａ，Ｓｈａｓｈｉ，Ｈｉｓｔｏｒｙ　ａｎｄ　Ａｒｃｈａｅｏｌｏｇｙ　ｏｆ　Ｉｎｄｉａ‘ｓ　Ｃｏｎｔａｃｔ　ｗｉｔｈ　ｏｔｈｅｒ　Ｃｏｕｎｔｒｉｅｓ－Ｆｒｏｍ　Ｅａｒｌｉｓｅｓｔ　Ｔｉｍｅｓ　ｔｏ　３００　ＢＣ，Ｐ。１５４。　Ｄｅｌｈｉ　Ｂ。　Ｒ。　ＰｕｂｌｉｓｈｉｎｇＣｏｒｐｏｒａｔｉｏｎ，１９７８）估计这个民族是古代居住在中国西藏或是云南的少数民族，他们最早于印度发生交往。　ｈｔｔｐ：／／ｗｗｗ。ｉｎｔｌｓｈｏｐ。ｃｏｍ３、希腊文献中早已出现“赛里斯”（Ｓｅｒｅｓ），意为“中国人”。据公元前４世纪希腊人亨利克泰夏斯（Ｃｔｅｓｉａｓ）等的记载，这种称名就是起于　“丝（Ｓｅｒ）”（参见前文所引的米勒【Ｍｕｌｌｅｒ＊】版本，１８８４年巴黎迪多书店版本，转引自【法】戈岱司编《希腊拉丁作家远东文献辑录》，耿N译，中华书局，１９８７年版）。也有可能如公园２世纪的罗马人包撒尼雅斯（Ｐａｕｓａｎｉａｓ）的《希腊志》中所说，就是“他们国内生存的一种小动物，希腊人称之为’赛儿‘（Ｓｅｒ＊）”。我们可以肯定，希腊文中是以Ｓｅｒｅｓ来称中国的，其起于蚕与丝的本意。<br />
　　与此相异的是，关于红海、波斯湾、印度半岛的有关文献中，出现了Ｔｈｉｎａｉ，《厄立特里亚海航行记》（Ｐｅｒｉｐｌｕｓ　ｏｆ　ｔｈｅ　Ｅｒｙｔｈｒａｅａｎ　Ｓｅａ）中又写道：<br />
　　经过这一地区之后，就已经到达了最北部地区，大海流到一个可能属于赛里斯国的地区，这一地区有一座很大的内陆城市叫做泰尼（Ｔｈｉｎａｉ）。那里的棉花、丝线和被称为Ｓｅｒｉｋｏｎ（意为丝国的）纺织品被商队陆行经大夏运至婆卢羯车（Ｂａｒｙｇａｚａ），或通过恒河而运至利穆利。<br />
　　（《厄立特里亚海航行记》，本书作者佚名，据说是埃及之希腊人，大约成书于公元１世纪末，记述红海、波斯湾与印度半岛的航行。参见法布里西尤斯版本，１８８３年莱比锡出版）这本书中出现了“支那国”（Ｔｈｉｎａｉ），这是较早出现的与希腊人的“赛里斯”<br />
　　Ｓｅｒｅｓ稍有差异的中国称名。我们注意到，这一名称是与丝绸运往印度的记录有关的。起于希腊罗马人的中国称名中，基本以Ｓｅｒｅｓ为本，并且衍生出各种称名，如见于多种著作中中国丝绸Ｓｅｒｉｋｏｎ，公元２世纪罗马人阿克伦（Ａｃｒｏｎ）的《颂歌》中所说的“赛里斯人织物”（Ｓｅｒｉｃｕｍ），见于公元２世纪的托勒密（Ｐｔｏｌｅｍｅｅ）《地理志》中Ｓｉｎａｉ。<br />
　　４、我们比较一下集中古代文字中的关于中国的称名，就可以看出其中的联系。<br />
　　最早的也是最重要的东方古代文字当推梵文。梵文中“支那”为Ｃｉｎａ（考铁利《论政治》即“Ａｒｔｈａｓａｓｔｒａ”，并且说明中国丝绸贩运到印度），　“支那”在梵语中也是Ｃｈｉｎａｓ（参见《玛奴法典》Ｌａｗｓ　ｏｆ　Ｍａｎｕ）。这样我们可以断定，梵语中的Ｃｉｎａ与Ｃｈｉｎａｓ是一个词，而不是两个词，都是起于丝绸的中国称名。汉语中的“支那”其实是对梵文的音译，《大唐西域记》卷五所说“摩呵支那”，《宋史》卷四九十《天竺国传》<br />
　　中所载“近闻支那国内有大明王”，系来自梵文，Ｍａｈａｃｈｉｎａｓｔｈａｎａ是从古梵文所变化出来的。<br />
　　东方文字中，闪族语系是另一个最重要的古代语系，腓尼基语就是闪族语系，它以后影响到古代希腊的语言。在希伯来文中，《圣经旧约》的《以赛亚篇》中也有提到中国：<br />
　　看哪，这些从远方来，这些从北方来，从西方来这些来自赛那姆（Ｓｉｎｉｎｍ）。<br />
　　这里的Ｓｉｎｉｎｍ就是中国，明显即“支那”，其读音与希腊文中的“赛里斯”的第一个音节是相同的。我们可以说，闪族语是介于梵语与希腊拉丁文之间的一种中国称名，其第一个音节“赛”同于希腊，而第二个音节“那”则通榆梵文，这说明它可能是从梵文向希腊文的过渡。<br />
　　其他中东与中亚的古代语言恰与希伯来文、印度文相呼应，如中国出土的《大秦景教流行中国碑》中，用叙利亚文写下了中国的名称：支那斯坦　Ｚｈｉｎａｓｔａｎ。这一称名基本同于希腊拉丁文中的读音。<br />
　　栗特文（Ｓｏｇｄｉａ）是中东的一种重要文字，英国斯坦因所整理的栗特文书中，据发过葛底奥特（Ｍ・Ｒｏｂｅｒｔ　Ｇａｕｔｈｉｏｔ）的研究，中国记为Ｃｙｎｓｔｎ（的参见《通报》，１９１３，第４２８页），这也可以证明，中国丝绸在西运中，曾经通过印度与西域，众所周知，古代西域是众多宗教汇聚地区，来自印度的佛教、伊朗的拜火教等都曾在西域流行，并且传入中国。这就是汉代以后的丝绸之路三条要到。所以栗特文的记录也是一个证明。<br />
　　这样我们可以有一个总结，中国丝绸古代通向希腊最早是通过西域、印度和中东，最后才到达雅典与罗马，因此，中国的称名也随着这些古代民族的语言而传播。所以最早的中文称名可能来自梵文与希伯来文，这两种文字中都以“丝”的发音来称呼中国，即梵文中的Ｃｉｎａ与希伯来文中的　Ｓｉｎｉｎｍ，这两种重要语言的称名翻译到希腊文与拉丁文，形成“赛里斯”Ｓｅｒｅｓ之名。这种称名的形成除了语言的音译外，相当重要的是丝绸贸易本身所形成的影响，名与实相得益彰，这就是“支那”一词指中国的来源。这一过程的音转关系与对音关系如下：<br />
　　古希腊文　Ｓｅｒｅｓ梵文　Ｃｉｎａ　希伯来文　Ｓｉｎｉｎｍ叙利亚文　Ｚｈｉｎａｓｔａｎ梵文中的Ｃ是不可能成为Ｃｈ的，而与希腊文中的Ｓ实同一读音，所以希腊文中的读音与梵文中相同，印欧语系中的读音基本是“丝”的读音。而叙利亚自古以来由于地处中国、波斯和欧洲的中介地位，文字很可能受到波斯文的影响。古代波斯人用伊朗语，伊朗语中没有送气浊辅音，发生Ｓ－Ｚｈ的音转。这就是“秦”　Ｚｈｉｎ或是Ｃｈｉｎ的读音的来源，后人附会为秦的名。<br />
　　这样，支那一词的来源就基本清除了，顺便说到，《圣经・旧说》的《以赛亚书》<br />
　　属于后先知书，是公元前８世纪至公元前５世纪的先知们所发表的时事政论，从年代来看，也都早于中国秦朝的建立，甚至早于秦国的存在。这也是西方中国古代称名支那不可能为“秦”的音译的重要证据，前人多忽略了这一点，笔者认为尤其重要，有必要特别指出。　ｈｔｔｐ：／／ｗｗｗ。ｉｎｔｌｓｈｏｐ。ｃｏｍ综上所述，从中外文化交流的历史来看，“支那”本义为丝，说明中国丝绸之路是有世纪意义的。近年来关于中国丝绸之路的研究表明，早在张骞“凿空”之前，中国丝绸可能已经远行希腊与印度，因此，这条以后日益发达的丝绸运输道路对于东西方文化交流的作用，日久弥深。证实这一历史背景之下，才可能索解中国称名的历史。<br />
　　另外要说明的是，这个词从本义来说，是没有贬义的。以后由于历史原因，受到一些人的曲解，“支那”被认为含有贬低与歧视的色彩，要恢复本义，必须经过这种正名。这也正是当代中国人的义务，无须赘言。<br />
　　作者：　方汉文，原文载于《寻根》杂志本文网址：ｈｔｔｐ：／／ｃｈｉｎａ。ｃａｌｓｕｎｓｈｉｎｅ。ｉｎｆｏ／ａｒｔｉｃｌｅｓ／２００４／０８／０１／０００５８６。ｐｈｐ<br />
