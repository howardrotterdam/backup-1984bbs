													<h2>中国应该更名支那</h2>
						
						
																											<div id="postmessage_69660" class="t_msgfont">一点五<br />
<br />
我至今对“美国”、“德国”、“英国”等等西方国家的中文译名感到奇怪：大清国的那些翻译家们，为什么要对他们眼里的“夷狄之国”，一个个冠以这么美好的国名？<br />
<br />
从历史来看，“中国”则从来象征着对少数民族的歧视，正如费正清说：一般说来，一个中原王朝建立后，它的主要统治区才可以被称为“中国”，而它所统治的边远地区以及统治范围之外是夷、狄、蛮，不是“中国”。<br />
<br />
今天我们所谓中国的得名，源于中华民国。1912年中华民国建立以后，中国才成为国家的正式简称，成了国家的代名词。而中华民国，至少在大陆执政时期，一直是一个“万恶的旧社会”，所以解放后很长一段时间，中国人都是耻于自称“中国”的，一律称“新中国”。<br />
<br />
日本人当年也拒用“中国”这个称呼，理由有四： 1、“中国”是个傲慢的名称；2、作为历史上的通称，除“支那”之外，便无更好的名称；3、“支那”，亦即China，是世界性的名称；4、日本国内有“中国”这个地名。 <br />
<br />
日本人的这些观点，不能说没有价值。世界是从瓷器即“China”开始知道中国的，而且瓷器也一直是中华民族最引以为豪的商品之一。用它的译音“支那”作为“中国”的指称，能引起全世界各国人民的历史回忆与共鸣。<br />
<br />
有网友认为，“支那”近似“拆哪”，寓意破旧立新，富有时代精神，与“新中国”不谋而合，也喻意当代中国进入了革故鼎新的伟大时代。还有网友进一步提出，“支那”的同音“芝娜”，能让人浮想联翩，心生和谐。<br />
<br />
因此，去“中国”，复“支那”，实在是对中华民族传统文化的最好保护与弘扬。此外的一个好处是，台湾的“一中一台”也好，“两个中国”也好，将因此而失去工作对象，台独自然垮塌，这对中华民族，当然是大好。</div>

							
							
							
							
