													<h2>记得小时候 年轻人讲方言代表的是一种混混文化</h2>
						
						
																											<div id="postmessage_732308" class="t_msgfont">因为至少在我小时候生长的环境里，年轻人说方言代表的是一种混混文化。即学校中那些喜欢坏孩子，以及社会上的那些和学校里坏孩子勾结的无业游民。在西安，大多数人使用两种方言，一种是陕西话，而另一种则是河南话。使用河南话的人群主要分布在城北以及铁路系统中。<br />
<br />
在学校，尽管有些老师因为年龄大使用方言上课，同学之间则全部是使用普通话。我不知道全国其他地方如何，至少在西安很多长辈，家长们之间使用方言交流，但是一旦他们和孩子说话都会说普通话。由此产生了一种年轻人讲方言可以显得自己很厉害，很不好惹的奇怪状况。在学校，讲方言的孩子多半是一些很混或者希望自己能够很混的。这些人在放学之后和在学校门口等他们的社会人士基本上是使用陕西话作为“官方语言”。滑稽的是，一旦你可以讲河南话，并且和你接触的社会人士也是讲河南话，那么在其他人眼中，你混的等级立刻会高于那些个仅仅只讲陕西话的孩子。我老家是河南，因此我小时候便会使用两种方言，这使得我上学的环境“安全“了不少。因为每当在街机厅或者去街机厅的路上碰到劫钱的，如果使用普通话和他们对话则大多逃不过被叫去厕所搜身的厄运。可是如果一张口便是一口地道的河南话，不但发音要像而且最好要能够使用河南话中特有的词语，那么大多数情况下如果你坚持没钱则都会逃过一劫。这些东西并不是我个人的主观认识，实际上就拿西安来说陕西话在很多场合中要比普通话”更好使“。因为方言代表着本地化，市井，甚至是混混的文化。大学里，一些本地学生为了使自己看起来不那么”乖“，便开始大量使用陕西话。只可惜发音很不地道，让人听了起鸡皮疙瘩。同时会起到一个反作用，让人觉得这个人很嫩。<br />
<br />
一开始我以为这种对于方言的认识仅仅限于我所生长的环境，甚至可以缩小的我所生长的那几条街道。但是后来通过电视，电影，书籍等等的影响，我发现这个实际上这是一种普通的现象，比如《艋{》中流氓都是讲台语；对人的形容”很台“所代表的意思。这就说明台湾社会中方言实际上也是代表了一种混混文化。方言也许可以代表很多东西，但是我想里面至少会包含混混文化这一条。后来，我又看到了很多新闻，说的是很多大学的新生因为自己的普通话讲不好，而很少和同学交流，甚至羞于开口。这种情况的产生正是由于多少年来各种媒体从来不去使用方言，甚至有意的扼杀方言。从而导致了在全国各地人们普遍的认为方言代表的一种很混，很土，甚至是落后的文化。而”京腔“则显得有文化的多。<br />
<br />
不过现在，这种情况似乎反了过来。在经济发达的地区，方言似乎又牛B了起来，而且成为了区分本地人和外地人的一个符号。你不会说我们的方言？哦，那你显然不是我们这里的人，即使你有户口，那你也终究是个外地人。这仅仅是一种变相的血统论罢了。于是，本地人起来开始保护方言或者说开始维护和支持血统论，这不禁让我想起了五十多年前，只不过当时的那些人是依靠自己的父辈所处的政治团体来划分人与人的区别，而现在则是靠地区和语言。以前那种觉得方言土且混的感觉不复存在，取而代之的是一种崇高的”历史使命感”――我们是在延续一种文化。在外人看来，这种说法更加令人恶心，犹如小偷强调自己的职业是平衡贫富差距一般。文化是无法被强权所消灭的，正如今天的繁体字一样。但是为了反对强权而拿繁体字说事儿，无限抬高繁体字的作用和历史意义，在你看来是不是也是一种傻逼的行为？所以，请别在方言上犯同样的错误。</div>

							
							
							
							
