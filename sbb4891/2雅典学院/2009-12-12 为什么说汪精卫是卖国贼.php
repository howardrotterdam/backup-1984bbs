													<h2>为什么说汪精卫是卖国贼</h2>
						
						
																											<div id="postmessage_369430" class="t_msgfont">汪精卫这个人很傻左。不过，我要是说”汪精卫是左派，所以是卖国贼“，恐怕有些人肯定不会接受，所以关于汪精卫这个方面的问题，我就不追究了，说别的。<br />
大体上讲，为了照顾被占领区的人民，总要有人去和日本鬼子合作。但是，合作也要看什么人，流氓无产阶级固然无此资格，象汪精卫这样的二号人物也绝无必要，一般乡绅什么的就足够了。大政治家与敌人合作等于资匪，象法国的贝当战后即被判处死刑；虽未执行，但也足够说明他被认定为卖国贼了。<br />
支持汪精卫的人，往往说”汪精卫自愿为国家作出牺牲，让中国能在同盟国和轴心国之间脚踩两只船“。从汪精卫的个性上看，这个确实有可能，至于”爱民“之心（傻左都”爱民“）当然也无须否认。但是，汪精卫的所有行为，却完全建立在他对世界局势和日本人诚意的错误估计上。汪精卫认为，美国政府会永远放任日本侵华，甚至美日开战以后，他也不相信美国方面压倒性（大约10倍）的物质优势必然能够主导战局。至于日本政府的诚意，汪精卫也是估计过高，结果公认是上当的。<br />
现在的问题是：一种完全错误的认识导致的完全失败的政策，如果出于善意，是否可以原谅？<br />
我的回答是：当然不能。不然的话，跑步进入共产主义，是不是也可以原谅呢？<br />
一个人，只要足够白痴，他就可以认为跑步进入共产主义是可以增进人民的福利的。但是，作为一个政治家，这却是不可原谅的愚蠢。跑步进入共产主义的失败不可原谅，投日失败也不可原谅。汪精卫就是卖国贼。<br />
（如果成功，当然另当别论，如果跑步进入共产主义成功，人民从此过上了共产主义理想中的生活，即便并非所有人的希望，但也谈不上不可原谅的罪行吧？如果汪精卫投日被历史证明是成功的，当然也就无所谓罪行了。）<br />
这里面的关键就在于：我们不能把政治家当普通人来要求。象”政治家也有隐私权“啦，”换你当官也当不好“啦，”汪精卫虽然愚蠢但可以原谅“啦，之类，都是把政治家降低到了普通人的标准，等于是放弃了对政治家的监督，让他们只有权力而无义务。<br />
当然，我们在认定汪精卫是卖国贼的同时，也必须强调：国家的灾难，并不是因为有汪精卫这样的卖国贼才造成的。至于这个原因到底能追究到谁头上（日本人，美国人，俄国人，毛，蒋等等），各占多少，就离题了，不论。</div>

							
							
							
							
