																											<div id="postmessage_369694" class="t_msgfont">　中日战争爆发后，日本对战局的形势有三种看法∶一、快胜论∶认为中国军队不堪一击，可以在“三个月内解决支那问题”；二、持久战论∶认为中国国土辽阔，战场的纵深太大，日军只能占据一些军事据点，不可能有效地控制整个中国。中国政府如果坚持不降，中日战争将成为旷日持久的持久战；三、外国干涉论∶认为中日战争将导致第三国的武装干涉，日本会在外国的政治军事压力下被迫退出中国。 <br />
　　随着战局的发展，证明“快胜论”的预言是错误的，但外国也没有积极干预中日战争，所以“持久战”的预言成为现实。日本非常不愿意打持久消耗战，持久战拖得越久，对人口资源丰富的中国就越有利，日本将在消耗战中拖垮。到1938年底，日本用在中国战场的兵力已增至24个师团，军费激增，财政困难，税收加重，国内危机日渐困重。在这样的情况下，日本内部也出现了尽快解决战争的呼声。 <br />
　　在1938年初，日本还寄希望于用高压手段迫降中国政府，1938年1月16日近卫首相发表了《不以国民政府为对手》的强硬声明。1938年10月，日军本想在武汉战役中一举歼灭中国军队主力，但中国军队主动撤退，中国政府也迁都重庆，使日本通过武力高压迅速解决中国问题的企图破产。此后，日本政府内要求“重新检讨对华政策”的呼声抬头，1938年11月3日，近卫首相发表了第二次对华声明，一改过去的“不以国民政府为对手”方针，表示∶“帝国所期求者即建设确保东亚永久和平的新秩序。只要国民政府抛弃以前的一贯政策，更换人事组织，取得新生的成果，参加新秩序的建设，我方并不予以拒绝”。 <br />
　　1938年12月6日，陆军省参谋部公布《1938年秋季以后的对华处理方略》，决定今后的对华总方针∶“以攻占汉口、广州为行使武力时期，今后自主指导新中国建设，切戒急躁。为此，目前应以恢复治安为根本，其他各项施策都应与此相适应。”对华总方针还规定军事上“以不扩大占领区”为宗旨，确定新黄河、合肥、芜湖、杭州一线以东的占领区为治安地区，以西为作战地区。在不扩大占领区的原则下，对国民党军实施有限的局部进攻，同时加强航空作战，轰炸重庆国民政府大后方及中国国际补给线，打击中国的抗日意志，促使国民政府崩溃。 <br />
　　1938年底以后，日本政府和军部的对华方针由“军事打击为主，和平谈判为辅”，改为“军事打击为辅，和平谈判为主”，由否认重庆国民政府改变为承认重庆国民政府。1938年11月以后，日本政府开始考虑作出一定让步的情况下，和中国政府谈判，结束战争。1939年3月，日本新首相平沼在国会演说中公开表示说∶“蒋介石将军与其领导之政府，假使能重新考虑其反日态度，与日本共同合作，谋东亚新秩序之建立，则日本准备与之进行中止敌对行动的谈判”。 <br />
　　中日战争爆发后，中国对战局的形势有四种看法∶一、胜利论∶认为中国军队已具有击退日军的实力，可以击败日本的军事进攻，蠃得军事上的胜利；二、亡国论∶认为中日战争的结果将导致中国亡国；三、最后胜利论∶中日战争成为持久战后，最终将引发外国的武装干涉介入，中国在外国的帮助下最终战胜日本；四、和平救国论∶中日战争成为旷日持久的消耗战后，日本也难于忍受长期的消耗战，所以日本不能灭亡中国，不得不与中国进行停战和谈。而中国也应该积极响应和谈，尽快结束在中国土地上进行的破坏性巨大的战争，减少中国国力的损失。 <br />
　　日本对华政策改变后，开始发出和谈的信号和放出和平的气球，这正好与汪精卫等人的“和平救国”论看法不谋而合，国民党内对“是战还是和”的问题上，发生了重大分裂。但由于“主战”和正义联系在一起，“主和”不免戴上投降叛国的汉奸帽子，所以国民政府内“主战”派还是占绝对多数。在此情况下，汪精卫等“主和派”开始了自己独自的“和平工作”。不过蒋介石在“主战”和“主和”之间持机会主义态度，一方面他高唱抗战到底的高调，另一方面对汪精卫等人的“和平工作”睁一只眼闭一只眼，为中日战争的结局留一条后路。蒋介石并没有制止和惩处参与“和平工作”的官员，使汪精卫的“和平工作”得以顺利进行。 <br />
　　国民政府外交部亚洲司日本科科长董道宁，通过私人关系与在上海的日本南满铁路驻京办事处主任西义显、同盟通讯社上海支局长松本重治等人进行接触，摸索与日本和谈的可能性。西义显和松本重治劝董道宁直接到日本与军方人士会谈，于是在松本的安排下，董道宁于1938年2月，即近卫首相发表“不以国民政府为对手”声明的一个月后，来到日本与陆军参谋本部中国课课长影佐祯昭大佐进行了会谈。 <br />
　　董道宁是一个小小的科长，肯定是在某个上层人士的指派下到日本秘密谈判的，但董道宁到底是受谁的指派直到现在也还搞不清楚。 <br />
　　参谋本部中国课长的影佐祯昭大佐是日本的主和派，是早期参与和平工作的唯一军人。董道宁向影佐询问日本改变“不以国民政府为对手”政策的可能性，影佐说∶军部中也有很多人反对近卫首相发表那样的强硬声明，但近卫首相的声明亦是事出有因，不可能那样简单地取消。但影佐表示日本希望和平的人也大有人在，非常愿意与中国进行停战谈判，期望中国也能够表示出具有和谈的诚意。董道宁请影佐给蒋介石写封信，影佐认为他以一个课长的身份给蒋介石写信不太合适，于是他给在陆军士官学校的老同学何应钦和张群分别写了信，影佐在信中说∶“不以国民政府为对手的声明是一桩不幸的事件，董道宁此番来日本表示了中国的诚意，日本深为感谢”。 <br />
　　董道宁回国后将影佐的信交给上司高宗武（外交部亚洲司司长），高宗武又将信转交给蒋介石的近侧宣传部长周佛海，周佛海再将信转交给汪精卫和蒋介石。不久高宗武向西义显转达了蒋介石向影佐祯昭的好意表示的谢意。周佛海又向蒋介石进言派亚洲司司长高宗武到上海“收集情报”，蒋介石同意高宗武到上海进行秘密外交，并从军委会秘密经费中每月支6000元作为高宗武的活动经费。周佛海不久又背着蒋介石，密派高宗武直接到日本会谈，高宗武有些担心∶“我去日本蒋先生知道吗？”周佛海说∶“你就放心去吧，一切责任由我承担。” <br />
　　周佛海之所以敢瞒着蒋介石和日方谈判，是因为周佛海对蒋介石抱有这样一种看法∶抗战后蒋先生一反他以往的表现，如果把过去蒋先生的行为贯穿起来进行研究，就会知道蒋先生的真正态度是“主和”的。由于举国上下高唱全面持久抗战，蒋先生不能不附合，而且比别人的调子更高，以便压服反对他的人，争取舆论的支持，蒋先生是被逼迫着走上他不愿意走的路。所以周佛海决定“以自己的胆识，把蒋先生的作法反过来加以灵活运用，等决心和平后，再向蒋先生报告。”周佛海认为蒋介石终究会明白他的一番苦心。蒋介石在知道高宗武未经他许可私下到日本谈判时，只是说∶“荒唐，荒唐”，并没有提出对高宗武进行处罚，更没有强调今后要严格禁止类似事件发生。 <br />
　　1938年6月高宗武在松本重治的陪同下来到日本，高宗武到日本后首先表示他并不是蒋介石派来的使者，而是中国主和派的代表。高宗武说∶“我首先想说明我的立场，为了实现和平，我准备不惜做出任何牺牲。为了中国，我最为重视的不是对日作战，而是对共产党的斗争。关于这一点，蒋委员长周围的一部分军人，主张即使把对共产党的斗争置之以后，也要把抗日看成国家的当务之急，我对此是反对的。但汉奸我可不当，我所奋力争取的仅仅是实现和平，这条界限是明确的。如果是不当汉奸的和平工作，那么有什么困难我都忍耐，但倘若这样做会成为汉奸，那么我马上就此作罢，即使说我中途脱逃也好，或说我是叛徒也罢，我都要退出。”后来高宗武果然因为害怕当汉奸，退出了“和平运动”。 <br />
　　高宗武怀疑蒋介石愿意冒卖国的罪名进行和谈，高宗武说∶“关于中日和谈的大义，如果从我的信念上来说，我不得不以汪先生为同伙。随着战祸的扩大，国民是不能忍受的。蒋先生冷酷，而汪先生温暖。”高宗武努力推荐汪精卫，他对影佐帧昭说∶“为了造成中日之间的和平，也许必须找蒋先生以外的人。除了汪先生之外，就不容易找到别人。汪先生早已痛感有迅速解决日中问题的必要，而国民政府内部终究不能容纳他的主张。为此，不如从政府外部掀起国民运动，开展和平运动，由此造成蒋先生听从和平论的时机，这样较为适当。” <br />
　　日本军方逐渐为中日战争的长期化感到头疼，对影佐的和平计划开始产生兴趣。1938年7月初在影佐帧昭的斡旋下，高宗武与日本陆军大臣板垣征四郎进行了会谈。会谈时高宗武提出由汪精卫出马，从政府外部掀起和平运动的计划，希望日本方面为汪精卫出马提供有利条件。日方同意以“不要领土，不要赔款”的优惠条件，支持汪精卫出马进行“和平运动”。高宗武于7月21日返回中国。 <br />
　　高宗武因未经蒋介石允许擅自赴日，而且此行得到的结果也对蒋介石不利，所以不敢回武汉，只向周佛海报告了日本之行的情况，并付一封致蒋介石的信∶“委员长钧鉴∶职于六月二十三日由香港秘密东渡，刻已平安返港。兹谨将职东渡日记及在东京会谈记录与职个人观感三项，分别呈阅。倘有可能以供钧座参考之处，则或可赎职擅赴之罪于万一”。周佛海先把高宗武的报告送给汪精卫，因高在报告中写有“日本希望汪先生出马”字句，周佛海征求汪精卫的意见∶“在呈送蒋先生以前要不要把这段删去？”。汪精卫则说“没有关系，他不是那种斤斤计较的人”。蒋介石看完高宗武的报告后，转交行政院副院长兼国防最高委员会秘书长张群，并嘱咐张群看完后转交汪精卫，他不知道汪精卫早已看过。过了三天蒋介石忽然大发雷霆∶“高宗武是个混蛋。谁让他到日本去的？” <br />
　　不过蒋介石仍然没有深究高宗武私自赴日谈判一事，这使周佛海更加敢于放手进行“和平运动”。不久高宗武因肺病复发住院，周佛海改派军委会政略部秘书主任梅思平接替高宗武进行秘密谈判。1938年8月29日至9月4日，梅思平与近卫首相的私人代表松本重治在香港进行了五次谈判，初步确定了日本政府以“不要领土，不要赔款，两年内撤军”为条件，支持汪精卫搞和平运动。梅思平将日方的内容转交汪精卫，汪精卫同意在此条件下出马主持和平运动。 <br />
　　1938年10月22日，汪精卫与周佛海、梅思平、陶希圣、陈壁君等人秘密召开会议，讨论和平运动的具体计划。当时他们计划的“和平运动”分五个阶段∶一、汪精卫离开重庆，在外地宣布下野，脱离国民政府。然后日本政府立即发表声明，提出不要领土，不要赔款，两年内撤军的条件，倡议与中国进行和谈；二、汪精卫以个人身份发出响应日本政府的和平倡议，建议国民政府接受日本的条件和平停战；三、云南等地的地方实力者通电响应汪精卫的和平号召，在云南等日军未占领地区建立新的独立政府；四、日本承认新政府并与新政府进行和平谈判，日军撤退回长城以北，将日军占领区转交新政府；五、新政府统一全国，实现中日两国间的真正和平。 <br />
　　汪精卫当初的这个和平计划是在日军未占领的地区建立新政府，然后新政府接收日军撤军后的占领地，在不割让领土、不赔款的条件下和日本实现和平。如果这个计划能够实现，对于中国人来说还是有相当的可接受性，汪精卫未必被国人责骂为卖国者。但后来由于种种原因，汪精卫的和平计划和他当初的设想出现了很大的偏差，汪精卫自己也成了牺牲品。 <br />
　　1938年11月3日，日本政府发表第二次对华声明，提出只要国民政府更变人事组织，日本就可以与中国进行停战谈判。日本所说的“更变人事组织”，就是指蒋介石下野。11月16日，汪精卫和蒋介石一同吃饭时，劝蒋介石下野促成中日和谈。 <br />
　　汪精卫说∶“使国家民族濒于灭亡是国民党的责任，我们应迅速联袂辞职，以谢天下。”蒋介石则反问汪∶“我们如果辞职，到底由谁负起政治的责任？”两人进行了激烈地争辩，最后蒋介石说∶“说什 都是一样，我们不必再争论了。我已经困了，要睡觉去了”。于是蒋介石将汪精卫一个人弃置不顾，自己回去睡觉了，这也促使汪精卫最后下定离开重庆的决心。 <br />
　　汪精卫和平计划的第一个关键问题是要得到云南政府主席“云南王”龙云的支持。汪精卫派陈璧君找龙云秘密商谈和平计划，龙云对蒋介石改编他的军队，调用云南的物质，削弱他的势力耿耿于怀。龙云对陈璧君说∶“汪先生是党国元老，在国内外声望极高，只要他登高一呼，应者必然云集于他的旗帜之下。蒋介石一贯阴险奸诈，排除异己，所以汪先生发动和平运动、另立新政府是天经地义之事。除了共产党和冯玉祥等少数人之外，都会拥护汪先生出来倡导和平事业，在国际上也会得到许多国家的支持。” <br />
有了龙云支持和平运动的许诺，下一个问题就是日本是否承诺宽大的和平条件。1938年11月12日，汪精卫派梅思平、高宗武等人为代表，日本派影佐帧昭、今井武夫等人为代表，在上海虹口公园附近的“重光堂”举行最后谈判，双方经过8天的讨价还价，最后达成了所谓“重光堂协议”。 <br />
　　（一）缔结华日防共协定，内蒙为特殊“防共”地区，日本在“防共区”驻兵。 <br />
　　（二）承认（默认）满洲国。 <br />
　　（三）日本废除在中国的治外法权，归还日本的在华租界。 <br />
　　（四）华日经济提携，在开发利用华北资源方面，为日本提供特殊方便。 <br />
　　（五）补偿因事变而造成的在华日本侨民所受的损失，日本不要求赔偿战费。 <br />
　　（六）恢复和平后，日军在两年以内撤军。 <br />
　　另外还有一个极为重要的问题，就是日本军方对中国分而治之的战略。日本军方感到一个统一的大中国对日本总是一个威胁，最好的消除中国威胁方法就是把中国分为数个国家“分而治之”。日军先在东北制造了一个满洲国，又于1937年12月在华北制造了一个以王克敏为首的北平中华民国临时政府，再于1938年4月在长江下游地区制造了一个以梁鸿志为首的南京中华民国维新政府。日方本来计划汪精卫出面建立一个局限于云南两广的地方政府，而汪精卫坚持必须撤销梁鸿志和王克敏等的地方政府，由汪精卫的中央政府统一领导中国。由于汪精卫在此问题上态度十分坚决，日方最后让步同意汪精卫的新政府统一领导中国。 <br />
　　梅思平把重光堂密约抄在丝绸上，缝在西装马甲里，于11月26日带到重庆，向汪精卫、周佛海汇报。但最后汪精卫比较动摇，决定了又推翻，推翻了又决定，在周佛海等人的劝说下，终于下决心选择“和平救国”的道路。汪精卫等人策划了一个脱离重庆的计划∶12月5日，周佛海先去昆明，12月8日，江精卫夫妇去成都，两日后到达昆明再去河内。日本政府于12月11日发表第三次对华声明，汪精卫随后在昆明、河内或香港中任何一地宣布下野。与此同时汪精卫也接到日方的通知，表示对重光堂协议无异议，希望一切按计划实行。 <br />
　　但多年跟随汪精卫的陈公博并不知道汪精卫与日本秘密商定的和平计划，汪精卫把在成都任四川省党部主任的陈公博请来，希望陈也参加他们的和平计划。陈公博看了“重光堂协议”后徒然变色∶“汪先生，这协议书是哪儿来的？委员长他知道吗？” <br />
　　周佛海说∶“委员长暂时不知。” <br />
　　汪精卫说∶“委员长不出来议和，我准备来议和，不然，焦土抗战，死且不知。” <br />
　　陈公博听后不安地说∶“汪先生，依公博之见，先生这样做恐怕是难为人理解的，还可能出大乱子。” <br />
　　汪精卫则向陈公博解释说∶“你我相处二十年，患难与共，我这次到外面去，和以前一样非常希望能够得到你的理解和支持。有路可走，我是不会离开重庆的，可现在只能离开重庆才能去谈和。我在重庆谈和，人家定会误以为是政府主张，这是于政府不利的。我离开重庆，那么和谈便是我个人的主张了，如果交涉得当，再来劝政府，由政府出面接受。如果政府仍然执迷不悟，那就只能撇开他们另组班子了。我与佛海一批人的良苦用心，将来是会得到国人理解的。” <br />
　　周佛海也劝陈公博说∶“历史的评价问题，牵涉到战局如何发展问题，很复杂，不是我们今天所能定得下来的。成事在天，谋事在人，要谋事总要付出代价和牺牲的。” <br />
　　陈公博见众人都同意，只好说∶“你们都统一意见了，我还有什么可说的？不过我总觉得这事还欠考虑，背着政府去和敌方谈和，无论结果怎样，总还有个立场问题。更何况还牵涉到要组织另一个政府问题，而日本方面是否有此诚意？此事须得慎重，最好放弃。”不过最后陈公博表态∶“汪先生走到哪里，我跟到哪里。我不想留在四川，眼看众人在我面前骂汪先生，更不愿被别人看成是汪先生留在政府里的密探。” <br />
　　1938年12月8日，汪精卫准备飞离重庆的时间，本来是蒋介石外出不在重庆的日子，但12月8日蒋介石突然从桂林飞抵重庆，打乱了汪的行动计划。汪精卫只好暂时留在重庆，等待时机。12月18日，蒋介石飞往陕西出席军事会议，汪精卫以外出演讲为名，偕夫人陈璧君、秘书曾仲鸣、女儿汪文惺和未婚夫何文杰等人飞往昆明。汪精卫到达昆明后，云南省政府主席龙云率领省、市各厅局官员列队相迎，后面是一排排学生和市民夹道欢迎。当天晚上，汪精卫与龙云作了一次恳切的谈话，汪精卫把自己和平运动的计划告诉龙云，说到达河内后即公开提出与日本议和的建议，接着由龙云发表拥汪通电，然后四川、西南和两广各实力派也纷纷行动起来，造成以汪为首的和平运动区，一面与日本交涉和平，一面也促使蒋介石的重庆政府议和。龙云听后满口表示赞同。 <br />
　　12月19日，汪精卫夫妇、周佛海、陶希圣、曾仲鸣等一行十余人，乘龙云代为包租的专机离开昆明，飞抵法属殖民地越南的河内，迈出了和平运动的第一步。据说汪精卫在离开重庆前，给蒋介石留下一封长信，信中最后写道∶“君为其易，我任其难”。在当时的情况下，“主和”的道路要比“主战”困难更多。 　　 <br />
　　汪精卫“主和”难，日本的“主和派”也阻力重重。由于这次发表的声明是正式声明，需要陆、海、外务三省协商后拟成正式文本，然而首先在最重要的参谋本部发生了意见分歧，参谋本部的要员坚决反对明确指出日本撤兵的日期，声称战胜国万不可做出向战败国许诺撤兵日期这样有损于国家威严之事，这样做太对不住前线浴血奋战的士兵。最后近卫向强硬派妥协，在声明中删去了“重光堂协议”中最为重要的日本撤兵一项。近卫在得知汪精卫于12月19日到达河内后，于1938年12月22日发表了第三次对华声明。 <br />
　　日方和平运动的发起者之一犬养健在他的回忆中，评论近卫的第三次对华声明时说∶“不要求领土，不要求赔款，归还外国租界，撤销治外法权等，如果按照重光堂会议的精神发表，确实会引起中国人的共鸣。可是，这次声明竟把最为重要的撤兵约定忘得一干二净，实在让人担心汪精卫和平运动会走上歧途，我深感失望。” <br />
　　日方和平运动的另一个发起者西义显听到近卫修改“重光堂协议”内容的声明后感叹说∶“日本把汪精卫欺骗了”，松本重治对近卫的声明内容也感到愕然∶“感到和平运动的未来已经出现了暗影”。 <br />
　　汪精卫对近卫修改“重光堂协议”内容的声明也深感不满，但他亲身体会“主和派”的艰难，近卫发表这样主和口气浓厚的声明，也一定有他的难处。虽然近卫声明删去了最为重要的撤兵内容，但在不要求领土、不要求赔款、归还外国租界、撤销治外法权等方面基本上符合“重光堂协议”，所以汪精卫决定按重光堂协议的规定，发表声明响应近卫的和平倡议。 <br />
　　1938年12月28日，汪精卫发出《致中央常务委员会国防最高会议书》，要求蒋介石和国民政府认真考虑日本政府的和平倡议，其原文如下∶“兹有上中央一电，除拍发外，谨再抄呈一纸，以备鉴察。本月9日，铭谒总裁蒋先生，曾力陈现在中国之困难在如何支持战局，日本之困难在如何结束战局，两者皆有困难，两者皆自知之及互知之，故和平非无可望。外交方面，期待英、美、法之协助，苏联之不反对，德、意之不作难，尤期待日本之觉悟，日本果能觉悟中国之不可屈服，东亚之不可独霸，则和平终当到来。” <br />
　　“凡此披沥，当日在座诸同志，所共闻也。今日方声明，实不能谓无觉悟。犹忆去岁12月初南京尚未陷落之际，德大使前赴南京谒蒋先生，所述日方条件，不如此明划，且较此为苛，蒋先生体念大局，曾毅然许诺，以之为和平谈判之基础。其后日方迁延，南京陷落之后，改提条件，范围广漠，遂致因循。今日方既有此觉悟，我方自应答以声明，以之为和平谈判之基础，而努力折冲，使具体方案得到相当解决，则结束战事以奠定东亚相安之局，诚为不可再失之良机矣。” <br />
　　“英、美、法之助力，今已见其端倪，惟此等助力仅能用于调停，俾我比较有利，决不能用于解决战事。俾我得因参战而获得全胜，此为尽人所能知，无待赘言。苏联不能脱离英、美、法而单独行动，德、意见我肯从事和平谈判，必欣然协助，国际情势，大致可见。至于国内，除共产党及惟恐中国不亡、惟恐国民政府不倒、惟恐中国国民党不灭之少数人外，想当无不同情者。铭经过深思熟虑之后，始敢向中央为此提议。除已另函蒋先生陈述意见外，仅再披沥以陈。伏望诸同志鉴其愚诚，俯赐赞同，幸甚，幸甚。专此，敬候公祺。 汪兆铭谨启” <br />
　　12月29日，汪精卫又发表了所谓的“艳电”。“艳电”这个词并没有什么特殊的意义，当时电报为了节省字数，把日期用一个字代替，12月29日的电报代号为“艳”字，故称之为“艳电”。汪精卫的“艳电”并不是宣布与日本“合作”的声明，而是以公开向国民党中央党部、国民党总裁蒋介石、国民党中央执监委员会提出建议的形式发表的。“艳电”的大致内容如下（手头暂时查不到“艳电”的原文）∶ <br />
　　近卫首相在最近的对华声明中指出日本对中国无领土要求，尊重中国主权独立，并提出了“善邻友好”、“共同防共”、“经济合作”三原则，表明日方已有和谈的诚意，希望蒋介石和国民政府以近卫声明为谈判基础，与日本政府交换诚意，以期恢复和平，汪精卫还特地提出近卫声明中没有提到的日本撤兵问题。“艳电”说∶“抗战年余，创巨痛深，倘犹能以合于正义之和平而结束战争，则国家之生存独立可保，即抗战之目的已达。” <br />
　　汪精卫出走重庆后，不少外国电台报道说∶汪精卫外出是代表国民政府军事委员会与日本谈判和平问题。12月24日，蒋介石让外交部紧急发表声明∶“汪兆铭无权和任何人谈判和平。”由于汪精卫在党内的地位，蒋介石担心汪精卫的出走影响人们对国民党的信心，最初只是对外声称汪精卫前往河内治病，为汪精卫返回重庆留下一条后路。 <br />
　　蒋介石先派正在河内洽办武器运输事务的外交部长王宠惠劝汪精卫回重庆，王宠惠说∶“委员长三番五次对人说，汪先生只是赴河内治病，现在回去，仍然名正言顺。” <br />
　　汪精卫回答说∶“谢谢重庆方面目前还给我留条退路。虽然这样，我还是不能回去，为什么呢？我这次离开重庆，只是对政局有不同意见，并不夹杂其他任何个人意气在内，这一点务请你们转告中央，请他们理解。在重庆，我要发表个人意见很不容易，我不离开重庆，这份艳电就不能发出，和平工作就难以开展。我的和平主张能否采纳，权操中央，我丝毫不勉强。如果政府出面主和，改变立场，我可以从旁做些协助工作，或者退隐山林不问国事都可以，但如果政府不转变立场，那我只能出面来谈和了。” <br />
　　汪精卫来到河内后，他的和平计划遭到很大的挫折。第一是近卫食言没有在声明中公布撤军的许诺。汪精卫本想近卫也有困难，准备今后再继续交涉撤兵一事，所以于1937年12月29日发表了响应近卫声明的“艳电”。没想到近卫内阁又于5天后的1938年1月3日宣布总辞职，使汪精卫陷入十分被动的境地。第二是龙云变心，没有按照当初的承诺发表对汪精卫和平运动的响应，迎接汪精卫在云南建立新政府。汪精卫本来并没有考虑在日军占领区建立政府，所以龙云的变心使汪精卫设想的和平运动完全泡了汤。 <br />
　　从汪精卫与日本人秘密和约的情况来看，并不是汪精卫一厢情愿的对日投降，而是双方都有和平停战意愿的情况下达成的双边协议。在整个秘密交涉的过程中，汪精卫并非十分主动，最初与日方进行接触的董道宁和高宗武也并非汪精卫派出的使者。而日方却比汪精卫主动得多，日方主动提出汪精卫出马的有利条件，汪精卫考虑再三后才答应的。而且汪精卫在秘密协议中提出日本撤军和建立统一中央政府的条件，都是对中国有利的。 <br />
　　如果日本当初不答应撤军的话，汪精卫也不太可能会离开重庆另立政府。然而日方却在汪精卫离开重庆走上不归路后，违背诺言收回了撤军的承诺。汪精卫的和平运动最能让人谅解的地方就在于日本撤军这一条，没有了日本撤军的保证，汪精卫的和平运动自然成为卖国投降行为。从这一点看，汪精卫也可以说是被日本人出卖了。 <br />
　　但日方倒也未必是有意出卖汪精卫，就象后来美国出卖对自己忠心耿耿的台湾国府与中共大陆建交一样，国家之间只有利益，却没有情谊，弱者总是处于被出卖的危险之中。 <br />
　　同意以日本的条件为基础进行和谈。汪精卫用这个实例说明∶与日本谋和并非自他开始，以蒋介石为首的领导集团早已秘密进行了。 <br />
　　汪精卫在文中责问蒋介石∶“主和是大家共同的主张，当时陶德曼大使奔走调停时，南京尚未陷落，蒋介石认为和谈可以进行，那么在近卫发表第三次声明后的今天为什么和谈就不可以进行？而且还要对主和的人横加诬蔑，诬蔑不足，还要夺其生命。再何况，一年多前日本提出的和谈条件十分苛刻，而蒋介石居然说不是亡国条件，同意作为谈判的基础，而今天近卫声明所提出的条件大大让步了，却为什么不可以作为谈判的基础？” <br />
　　近卫内阁倒台，继任者平沼首相见汪精卫发起和平运动后，西南省份的中国军政要人并没有象预计的那样通电支持汪精卫，因此对汪精卫的号召力和影响力产生了怀疑，但他们忘记了日方食言收回撤军承诺对汪精卫个人威望的打击。在1939年最初的几个月里，日本方面对汪精卫相当冷淡。但是河内发生汪精卫暗杀事件后，因为汪精卫毕竟是响应日方的和平号召才到河内的，日方召开了首相、陆相、海相、外相及藏相参加的“五相会议”，研究决定派遣影佐祯昭、犬养健等人前往河内营救汪精卫，帮助汪精卫转移到安全的地方，汪精卫在日本人的保护下到达上海，暂时住在法租界。 <br />
　　在轮船尚未到达上海之时，汪精卫就对影佐祯昭和犬养健说∶“到今天为止，我们的方针一直是以国民党为中心组织和平团体，用言论来揭露、指责重庆政府对日抗战的错误，宣传中日和平以拯救中国、拯救东亚，扩大和平运动的阵营，从而使重庆政府改弦易辙。原来设想在西南日军未占领的地区建立一个和平运动基地，但自从我离开重庆后情况发生了变化。现在我的想法也有了改变，我想不如推进百步，索性建立一个和平政府，通过日本政府对中国和平的诚意，用事实来证明抗战已毫无意义，从而促使重庆政府转向和平路线，这样比较适当。” 　　 <br />
　　汪精卫提出建立政府一事大出影佐等人的意料之外。当时日本政府只是吩咐影佐帮助汪精卫建立一个“反共救国同盟会”的民间组织，并没有设想汪精卫在日占区建立政府。所以影佐为难地说∶“诚然，这也确是一个好办法。但是事实上我们只是受命专来帮助先生转移到安全的地点而已。关于现在您所说的话，在公务上我没有答复您的资格。不过作为和平运动的同志，我还是发表我的意见。我认为，首先要看日本政府是否彻底实行近卫声明，这是前提条件。假如日本政府在这个态度上有所改变的话，那么先生的这个新建议将会完全失败的。我作为一个日本人说这样的话，不免有些奇怪，但这的确是一个需要慎重行事的事。” <br />
　　汪精卫说∶“我也有同感。比如说，重庆政府还不信任贵国，他们认为日本嘴上说得好听，实际上心中所想的却完全相反。各地希望和平的同志如今踌躇不前，也是出自同一理由。近卫声明是否实行，这是我们和平运动的分歧点。” 　　 <br />
　　犬养健问∶“不知汪先生在组织新政府时要向帝国政府提出什么要求？” <br />
　　汪精卫说∶“主要是希望日本政府坚持实行近卫首相的声明，如果不能践行声明，我必将遭到国人的讥笑，认为我是受日本的欺骗而离开重庆的。重庆方面也更不会相信日本政府，认为贵国口是心非，不守诺言。”汪精卫还说∶“倘若将来贵国政府要利用我们新政府的兵力，把我们看作日军的一翼，让我们与重庆军队作战的话，势必会引起内战，造成同民族间的流血牺牲，这是绝对要避免的。我们的最终目的，并不是要打倒重庆政府，而是在可能的情况下与之合作。这一点与所谓的反蒋运动有本质的区别。” <br />
　　汪精卫又说∶“对于中国人来说，抗日论也好，和平论也好，都是爱国心的表现，都是以不同的形式热爱国家。从另一方面来说，抗战论总容易让一般的中国人接受。与之相比，和平论几乎就等于卖国论，是很难让人接受的。从现在中国国民的心情来讲，哪怕是内地的一、两个省份因抗战而生存下来，人们也会确信，它将来必定成为国家振兴的根据地。这种主张最容易使血气方刚的中国青年热血沸腾。但是倘若青年们没有这样旺盛的精神，中国的将来和亚洲的将来也就令人担心了。由于这些缘故，在我们和平运动的征途上会遭受到相当的责难，要经常不断地被骂为卖国贼、汉奸，但我已做好挨骂的思想准备。我所盼望的只有一件事，就是日本的政策能按照与我们约定的那样在中国广泛宣传，到那时我们所经历的苦难，才会放射出异彩。我们在那个时候才能与抗日的青年们见面，互相笑着说明以前各自所走的不同的道路。” <br />
　　影佐祯昭被汪精卫的话深为感动，在他的回忆录《曾走路我记》中，给予了汪精卫最高级的赞词∶“汪氏的行动表现出他对支那、对东亚的热爱和赤诚，其崇高的精神、高洁的人格连鬼神都会为之感动。我除了真正地敬服感佩以外还能有什么呢？” <br />
　　不管怎样，汪精卫提出建立政府的“主动出击”，使日本政府一时感到为难。日本对中国的方针原来是分而治之，因为一个统一的大中国对日本总是一个威胁。日本于1937年12月在华北制造了一个以王克敏为首的北平中华民国临时政府，再于1938年4月在长江下游地区制造了一个以梁鸿志为首的南京中华民国维新政府。 <br />
　　日本并不打算在日占区建立一个统一的中央政府，所以对汪精卫提出在日军占领区建立政府的计划并不感兴趣。但汪精卫毕竟是中国政坛上的超重量级人物，日方还是认真考虑了汪精卫的建立政府设想。 <br />
　　1939年5月26日，汪精卫在上海召集高级干部会议，研究向日本政府主动提出“建立政府”的计划问题，参加会议的有周佛海、陶希圣、高宗武、梅思平等人。会议经过三天的讨论，拟订出了《关于收拾时局的具体办法》，准备以书面形式提交日方，汪精卫亲赴日本和日方谈判。《关于收拾时局的具体办法》提出∶先召开国民党临时全国代表大会，修改党章，重新制定政纲及政策，授权汪精卫组织中央政治会议，负责改组国民政府以及“还都南京”。该文件还规定∶在“国府还都”南京的同时，王克敏的北平中华民国临时政府和梁鸿志的南京中华民国维新政府自动取消，并宣布重庆国民政府为非法政府，其对国内发布的法令、对外缔结的条约协定一律无效。 <br />
　　1939年5月31日，汪精卫、周佛海、梅思平、高宗武等十一人，在影佐侦昭和犬养健陪同下秘密飞往东京。日方尽管对汪精卫的来访并没有多大热情，但还是给予汪精卫很高的礼遇，前首相近卫文吕和外相有田八郎亲往机场迎接。为了对外保持秘密，汪精卫被安排住在东京郊外的一所高级私人别墅。对汪精卫的来访，新任首相平沼骐一郎召开政府首脑紧急会议，讨论汪精卫提出在南京组建政府的要求，平沼说∶“原来我国政府鼓励汪氏出逃重庆是为了促进重庆政府内部分化而与帝国政府议和，这样战事就可能更加顺利一些。原先我们只是计划在经济上支持汪氏进行和平运动，现在汪氏要求我国政府支持他在南京组织中央政府，这是我们未曾想过的。” <br />
　　日本政府内部对汪精卫提出建立政府的设想意见很不一致，经过激烈的争论后，日方终于统一意见，同意汪精卫建立新政府。1939年6月5日，日本内阁五相会议拟出一个《建立新中央政府的方针》，该方针规定∶1、中央政府由汪精卫、吴佩孚、临时政府、维新政权共同组成，吴佩孚主军，汪精卫主政；2、新建中央政府根据《调整日华新关系的方针》与日本建立调整国交；3、中央政府必须具备一定的号召力，成立日期由日本决定；4、中央政府与地方政府的关系，以分治合作为原则；5、关于国民党和三民主义，在不妨碍亲日、满和反共的原则下，可允许存在。日方以《建立新中央政府的方针》为原则和汪精卫进行谈判，但日方的建立政府方针和汪精卫的建立政府设想相去甚远。 <br />
　　6月10日，汪精卫首先与平沼首相会谈，但没有什么实质内容。第二天与陆军大臣板垣的会谈，是这次谈判的最主要重头戏。板垣先提出陆军不赞成汪精卫在南京建立政府的意见，板垣说∶“难道除了南京，汪先生没有考虑设法在其它地方建立新政权吗？譬如在皇军占领区以外。” <br />
　　汪精卫回答说∶“原来我也是这样考虑的，只是形势发生了变化，才作出还都南京的决定。”板垣又说∶“南京已经有了维新政府，再由汪先生建立一个新政府，不是好办法。”汪精卫回答说∶“我们不说在南京建立新政府而是说还都南京。因为国民政府原在南京，重庆被称为陪都。还都能使重庆方面产生一种冲击力和向心力，使国内一切爱好和平之国民思和之心凝聚于故都南京。南京和平政府将是一个独立自主的中央政权，目前北平和南京的临时、维新两个政权应加以撤销，或自行解散，使之在中央政府统一领导下。” <br />
　　板垣听后反论说∶“分治合作是五相会议决定的原则，临时、维新两政府经过与汪先生协商可以考虑废除名义，但不能取消其内容及已形成的事实。”汪精卫则反论说∶“阁下的意思似乎与五相会议提出的内政问题交给中国方面的原则有了距离，如果只废除临时、维新两个名称而照旧保留其实体，要我出来收拾时局实感困难。因为中央政府有名而无实，势必会发生中央与地方之间的权限之争。 <br />
　　这样的中央政府实在不是我们所设想成立的独立自主政权，如果阁下要坚持，我只有退让或延期组织政府。” <br />
　　最后板垣作出一些让步，说只要汪精卫能够说服王克敏、梁鸿志自愿放弃政府，日方也不反对。 <br />
　　日本原指望汪精卫在日军占领区之外建立政府，分化瓦解中国的抗日力量。至于在日军占领区之内建立傀儡政府，王克敏、梁鸿志这样的人更加可靠。平沼内阁对汪精卫的建立政府计划很不积极，所以汪精卫这次秘密访日并没有太大收获。汪精卫回国后，曾走访王克敏、梁鸿志，试图说服两人同意放弃地方政府，支持成立统一的中央政府。但王、梁两人均支支吾吾，没有表示出愿意放弃地方政府的意思，这样汪精卫在日占区建立政府的计划又濒临死路。 <br />
　　汪精卫在河内发表“艳电”时，吴佩孚曾表示支持汪精卫的和平动给汪去信说∶“中日辅车相依，为友为敌，利害本昭然，但目前危亡抗战之中，更须化敌为友，因有史以来，从无久战不和之理。故自芦沟桥变起，身居故都，一直以启导和平为信念。”但汪精卫到北平约见吴佩孚时，吴佩孚态度很傲慢，结果会谈没有实现，不过不久吴佩孚忽然急病而死。 <br />
　　1939年8月，平沼内阁倒台，原陆军大将阿部信行出任首相。阿部上台后，开始积极支持汪精卫，同意取消临时、维新两个政府而由汪精卫领导成立一个统一的中央政府。1940年3月31日，汪精卫才正式“还都南京”，在日占区成立了一个统一的“国民政府”。 <br />
　　如果说日本人扶持汪精卫在日占区成立一个傀儡政府，倒不如说这个傀儡政府是汪精卫尽力“争取”而来，而且是来之不易。日本人一方面怀疑汪精卫搞和平计划是中国人的诡计，试图以此来缓解日本的军事压力。另一方面，日本也怀疑汪精卫等，人对日本的忠诚，尽管重庆开除了汪精卫等人党籍职务，但汪等原国民党人与重庆国民政府的高级官员仍有各种难于切断的千丝万缕联系，日本人担心汪精卫等人会在关键的时候反叛，会把日本的军事机密传递给重庆。事实上抗战期间重庆政府正是通过汪精卫政府获得了大量的军事情报。 <br />
　　日本人要在日占区建立一个傀儡政府，最重要的考虑之一就是傀儡政府对日本的忠诚可靠程度。从这方面来讲，王克敏、梁鸿志等人显然比汪精卫更加可靠。日本从刚开始就没有考虑过让汪精卫在日占区成立政府，只是希望汪精卫在日军未占领区成立政府。多亏影佐侦昭等和平运动发起人尽力奔走，向日本领导层说明汪精卫的诚意，最后日方才同意汪精卫成立政府的要求。 <br />
　　汪精卫本来性格清高，对金钱权力看得并不是很重，自动放弃了多次争夺权力的机会。但这次汪精卫却一反常态，竟然“别人不请我自来”，争着在日本人手下作一个傀儡政府的领导人，让人感到有些不可思议。难道汪精卫真是出于一种责任感，怕别人“卖国”把国家卖完，自己亲自来搞有限度的卖国？汪精卫的确说过∶“别人去送国还不如我汪某去送。别人送国是没有限度的，而我送国则有限度，不能让他们把国都送完。” <br />
　　不过从另一方面来看，汪精卫也许因为被人们所误解而难过。蒋介石不采纳他的和谈建议，他不得不出走重庆，在中国人的土地上建立一个新政府，和日本达成和平撤军的协议，救百姓于战火之中。这本来是一个可行的计划，他本人也不会因此被人们耻为卖国求荣的汉奸。可是没想到日本人临时变卦，收回了撤军的承诺，把汪精卫推进了汉奸的行列。 <br />
　　对此汪精卫是有苦难言，在当时他无法向别人解释清楚他离开中国是被日本人欺骗了。如果汪精卫隐遁欧洲，等于默认他干了卖国之事而出逃欧洲。汪精卫想干点什么事来进行挽回，因此汪精卫才迫不及待地要建立一个政府，想作一些对中国有益的事来改变人们对他的看法，澄清人们对他的误解。可是汪精卫的努力反而起了相反的作用，越陷越深，越洗越黑。政治仕海险处丛生，一步走错，当年最让人尊敬的革命老英雄就变成最大级的卖国贼。 <br />
