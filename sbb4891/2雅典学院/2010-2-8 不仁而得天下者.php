																											<div id="postmessage_472580" class="t_msgfont">　　By&nbsp; &nbsp;李海鹏<br />
<br />
　　有一回在长崎，我看了一个展览，内容是原子弹爆炸造成的灾难，我深受震撼，认识到这种悲剧决不该重演。我惟一不接受的是，那展览说原子弹扔得毫无必要，是美国人准备跟苏联对抗，拿广岛和长崎做实验呢。我问主办方的一个姑娘，听说过“一亿玉碎”的说法没有，就是日本陆相说，俺们有一亿人，宁可全部像樱花般飘零，也要跟你们盟军死磕。这姑娘说她听说过，但是究竟是什么意思不清楚。反正这类事他们都不清楚。他们就对对他们有利的清楚。日本人不想再打仗了，这一点我很信服。他们没碎成，扭脸又“一亿振兴”，把一个史上最烂的烂摊子收拾成了世界第二大经济体，这个我也很信服。但是他们选择性不清楚，让我很不信服。<br />
<br />
　　我得承认，破腹自尽是尊严的高度体现，我可做不到。樱花在头脑中飘零，同时集合了阳刚与阴柔之美，我也仰之弥高。日本的山川是如此秀美，乃至感染了人们的精神洁癖，他们建造的房屋庭院、竹子篱笆、石子小路，都富有自然意味和素朴的美感，真是一种令人赞赏的建筑艺术和生活方式。除了物价太贵之外，这国家也适合去玩，濑户内海的烟霞，可以悦人眼目，北方雪国的严寒，亦足以洗涤精神。要是哪天我做徒步旅行，那么首选之地就是日本。这也不是我的个人看法。在整个世界上，日本人的诚信、礼貌和对审美体验的刻骨铭心，都使得他们广受尊重。<br />
<br />
　　我还觉得日本人吃得好，同样是吃鱼，他们的解决方案比较出色，我不爱吃水煮鱼，又油又辣简直是个悲剧，更不爱吃奶油吞拿鱼和薯条之类的，完全没灵气的嘛，而生鱼刺身真是健康又美味。我还觉得日本妞好看，好看呢我也没想怎么着，但是很是欣赏她们身上透着的那一股教养，不像某些北京妞满口脏话还觉得自己有个性。我觉得教养这东西来之不易而且很贵，20克教养顶得上5吨LV皮包。<br />
<br />
　　说到这儿，可能有人已经怒不可遏：此人说不清楚是反日还是亲日，而且是个男权主义的猪猡！<br />
<br />
　　不是还没说到关键处嘛。对于时刻准备干掉家乐福和7-11的胆汁质的爱国青年们，我能说的就是，愤怒这种事，还是慢慢来为好，因为逻辑常常要比情绪来得迂缓和漫长。你一砖头把我打死了，然后才发现我是文天祥之后最真诚的爱国者，这事不仅悲哀，还太滑稽了是不是？<br />
<br />
　　说得太远了，关于日本要说的是，一个国家、一个政权和一种意识形态最终是否得到尊敬和支持，并不在于修院子和吃鱼的技巧，也不在于多么懂得宣扬自己为人民谋福利的辛苦劳顿，不在于煌煌武功和巧言令色，只在于道义。<br />
<br />
　　这个事漫说别人，我姥姥就深有体会。满洲国时期她在日本人的工厂做过工，日本监工来了，她就假装努力工作，日本监工走了，她就把工具一摔，呸，×你妈，小鬼子！这就是她的态度。日本人轴，或许真如李宗仁所说，在战术上无人能敌，在战略上毫无眼光，掠夺资源是真的，把中国东北当成了自己家也是真的，开了好多工厂，把沈阳建得比东京还繁华。日本开拓团原来都是稻农，一到沈阳，傻了，擦，国际大都市！这个大都市要靠中国工人的劳动来维持运转，可是他们磨磨蹭蹭。如今汉语里有个词汇叫“磨洋工”，正是我姥姥他们的作为所致。我问我姥姥，你信不信“王道乐土”之类的？她说，信个屁，要建乐土，怎么不自己家建去？<br />
<br />
　　你看，福泽谕吉以降的历代日本精英们创立了一个如此动听的思想体系，却连一个中国女工都骗不过。这是为啥呢？日本民族颇有优长之处，可是在二战中，没有人比他们输得更惨，吃的苦头更多，这又是为什么？历史可以有很多种解释，可是有一种，我看是最不可颠覆的，那就是孟子说的话：不仁而得国者，有之矣；不仁而得天下者，未之有也。最终，一个社会的最可警惕的危险就是，它看似拥有一切，钱有的是，权力有的是，武力有的是，可是仁……这个真没有。</div>

							
							
							
							
