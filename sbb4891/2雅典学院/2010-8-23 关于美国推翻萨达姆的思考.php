													<h2>关于美国推翻萨达姆的思考</h2>
						
						
																											<div id="postmessage_793224" class="t_msgfont">1、美国不是圣徒组成的国家，他推翻萨达姆也并不是基于什么正义，中东地区，有石油、伊斯兰教、以色列，后来又有大规模杀伤性武器，诸多因素，决定了美国进军该地，对美国的国家利益而言利大于弊，这是美国进军中东的主要原因。如果是人道主义，或者自由主义，他到非洲去会更加大有作为，造福的人数会更多。<br />
<br />
2、美国的政治文明程度远远高于其他地区，这使得被他侵占的地区，政治文明会有很大的提升。文明程度高的一个重要特点是文明底线的提高，比如保障新闻自由，保障基本人权。也许不少人会说，美国不是有关塔那摩，有虐俘等等吗？但是美国更有新闻界对此的曝光，有政府的反省。这种对新闻的保护就是文明底线的提高。所以，美国占领伊拉克后，肯定会提升当地的文明程度。<br />
<br />
3、伊拉克在美国占领和影响下，会不会逐渐走上政治文明之路？这个并不确定，伊拉克内部的种族矛盾，教派冲突，文化基础，对伊拉克未来的影响会远远大于美国的影响。而这些才是决定伊拉克未来政治成熟程度的关键因素。但是无论如何，美国彻底占领过伊拉克，和没有占领该地区，对未来的政治走向还是会起到相当大的作用。我个人认为美国的占领对伊拉克政治是利大于弊的，这个仅仅是个人判断。<br />
<br />
4、总体而言，美国既不是天使，也不是魔鬼。他出于自私的动机，短期内提升了伊拉克的政治文明程度。应该是美国入侵伊拉克的大框架。人道主义、自由主义从来就不是美国入侵别国的理由，伊拉克人民摆脱萨达姆的统治是其他因素机缘巧合的结果。<br />
