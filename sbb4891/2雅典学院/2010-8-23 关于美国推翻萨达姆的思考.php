事实上美国攻打伊拉克，既不能说美国只是为了普世价值，也不能说美国只是为了利益，这样看问题都太片面（二分法学多了吧）。很大层度上，美国的动机是多层次的，既有为了普世价值的成分，也有利益的成分。这才能解释为什么美国不去打非洲，不去打朝鲜，同时也解释了美国为什么总是对有威胁的极权国家指手画脚。为了能源，为了国民的利益，我想这其实没有什么可苛责的。而如果对于普世价值的破坏却视而不见的话，那就非常不应该了。20世纪初，西方国家对德国法西斯主义都视而不见，后果是多么的可怕。<br />
<br />
其次，关于毛，即使他是理想主义，为了美好的理想而去实践出恶行，即使他的动机是美好的，但也不能为他的行为正义化。原因在于：<br />
1、你的行动，应该把行为准则通过你的意志变为普遍的自然规律。<br />
2、你的行动，要把你自己人身中的人性，和其他人身中的人性，在任何时候都同样看做是目的，永远不能只看做是手段。<br />
3、你行动所依从的准则，要能同时使其自身成为像自然普遍规律那样的对象。<br />
――康德<br />
而毛行为中的道德价值，既不能成为普遍的自然规律，也不是以人性为目的，相反却是以人性为手段来达到其所谓的理想目的。退一万步说，即使毛的动机有为了理想的层面，那绝对也有为了私利，为了权力斗争的层面。这就是为什么我们现在会批评毛的原因。而对于美国的行为，虽然说仍存争议，但我想其正义和善的一面是大于自私的一面的。<br />
<br />
<br />
还有一点，看这种问题的时候，不要以结果来判断行为的是非。结果导向主义往往会忽略行为中恶的部分，例如“改革开放使物质生活水平提高，所以改革开放是正义的”这类的说辞，往往无视了其中的种种恶行。所以以伊拉克民主程度提高来判断美国的行为是正义还是邪恶也是片面的。<br />
