																											<div id="postmessage_810289" class="t_msgfont"><strong>转载自<a href="http://blog.sina.com.cn/s/blog_4701280b0100l4sf.html" target="_blank">http://blog.sina.com.cn/s/blog_4701280b0100l4sf.html</a>(作者韩寒)注：现在的文章标题已经改为2010年09月03日</strong><br />
这几天应该是独唱团第二辑上市的时间，由于最近台风登陆，所以还是有所延误，我们在为更好的质量和空间而努力，也为使他变成合法的月刊，而不是居无定所的绝唱团。在这个期间里，GQ杂志为我颁发了一个传媒人奖，这两年，无论是以往的《时尚先生》，还是创刊一年的GQ，都让时尚杂志的男刊不再难堪，很多其他媒体禁忌的名字，甚至出现在他们的名册里，GQ还曾被半路召回，这些都不是业内笑话，而是各种尝试，其实这些都应该是一个传媒人应该去做的。什么是做一个传媒人，我深深的思考过，在我们国家，其实就是做一个传达一下领导意思的媒人，做的不好就送你一个传票，然后就挖煤，这就是传媒人。在很长的一段时间里，我其实是一个被传媒的人，今年，我成了一个所谓的传媒人。好在这个奖项是年度传媒人，它并没有说年度最成功传媒人，我自认为是年度最失败传媒人。失败的原因，以后我会和你们讲来。我们知道，其实很多人宣称的“办一本杂志”“做一份报纸”，都是理想化的称呼，从程序上，这些都是非法的，合法的说法是――某个拥有党委的国有出版社或者杂志社聘请你和你的团队来打理一下他们的杂志。当然，唯一值得欣慰的是，没有我们，他们通常自己的打理的不大好。传媒是一个很大的名词，无论是传统的图书，出版，杂志，报纸，电视，电影，广播，互联网，电子阅读，甚至我们的耳语，打一个电话，贴一张海报，都是传媒，传媒的影响如此之大，谁都想控制它。但传媒其实不该控制在任何人手里，他应该是一片开放的天地，只要善待，谁都可以使用和拥有它，它的上司只有一个，法院，它的罪名只有一种，诽谤。这便是我理想中的传媒。可是理想是每一个人都会说，大家都爱听的，就好比你我都愿面向大海，春暖花开，说一次心里爽一次，却始终无法走出脚下的泥泽。但有念想的人总能走得更远，求生欲强的人总能活的更长，所以，我始终是高兴和乐观的。不过，作为所谓的传媒人，我们总需要不断尝试，我本该很高兴的去北京讲这些话，但因为实在不能过来，所以由老朋友黑狗达代我领一下奖，另外向现场的主编王锋先生，左小诅咒先生，姜文先生，刘北宪先生，白岩松先生和柴静女士问好。向程益中先生问好。<br />
<br />
 也向刚刚被跨省刑拘的谢朝平先生问好。<br />
<br />
 这些天一直没有更新这里，我发现在中国写杂文其实是一件很痛苦的事情，虽然这个国家给我们提供了大量的杂文素材，但是你突然发现，写了一阵子以后，当再有新闻出现，你恨不得直接写一句，观点请参考我某年某月写的某一篇。我们的领导群从这一批换成了那一批，治国口号从这一堆换成了那一堆，丰功伟绩从这个会变成了那个会，社会悲剧只是从这个人变成了那个人，换人不换事会让写作者觉得很痛苦，因为大部分的作家都讨厌反复阐述，结果事儿又是反复发生，对我们的遣词造句提出了很大的要求。因为我真的想一直继续写下去，不想让自己厌烦和麻痹。<br />
<br />
 昨天去看了《盗梦空间》，向大家推荐。<br />
