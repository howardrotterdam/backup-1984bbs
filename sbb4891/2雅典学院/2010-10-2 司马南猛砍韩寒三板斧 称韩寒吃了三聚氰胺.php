韩寒笃定认为，保卫钓鱼岛是有前提条件的：“如果大家一切都好，生活如意，老婆、孩子、房子、车子、工作、休息、健康、医疗，一切都能保住，闲情雅致之下，民族情操之下，又不愿韬光养晦，当然可以追保钓鱼岛。”（何为“追保钓鱼岛”，尚不解其意。）怕粉丝们听不懂他的话，把这段话翻过来，他又说了一遍：当然，“如果你自己还有什么保不住的，先把自己的保住再说，不要操那么前卫的心。”<br />
<br />
原来，“保卫钓鱼岛”在韩寒语境中有“前卫”之意，但是“这个前卫却并不时髦”，因而无需追求。相反，这个“前卫之心”不用你来操，应该避而远之，或者束之高阁。<br />
<br />
于是，韩寒捋一把前卫的长发，简短干脆地号召众粉：“先把自己的保住再说，不要操那么前卫的心。”<br />
<br />
韩寒思想还真的深刻且全面。<br />
<br />
这个年轻人不厌其烦地举例，要粉丝们先去保住老婆、保住孩子、保住房子、保住车子、保住工作、保住休息、保住健康、保住医疗……他的逻辑是，在一切“悉数保得住”的前提下，保卫钓鱼岛才“可能”是有意义的。<br />
<br />
之所以仅仅是一种“可能”，因为韩寒文章明确地提出，保卫钓鱼岛还有三个前提：第一，在有闲情雅致的情况之下；第二，在具备民族情操前提之下；第三，在不愿韬光养晦的前提之下。<br />
<br />
假使某一个前提没有满足呢？假使3个前提尚不具备呢？我们，还要不要保卫钓鱼岛？<br />
<br />
韩寒的标准答案是不言而喻的。<br />
<br />
读到这里的时候，我很奇怪，为什么要设立这么多的前提，才能谈保卫钓鱼岛呢？经过反复阅读才搞明白，原来钓鱼岛主权之争不是什么大是大非问题，因为就没有什么大是大非问题，因为“每个人都有其自定义大是大非的权力”。<br />
<br />
韩寒这种急速膨胀时髦的“是非观念”，不禁让我想起了前几天因为李一道长出丑而被媒体高度关注的“中国最大媒体”的从业者樊馨蔓姑娘。这个姑娘有一种可怖的说法，其“观念裸露”丝毫不逊于韩寒：“做记者的都知道，你无法传播事实的真相”，“因为就没有事实的真相，关键，是看你――我们，有资格可以阐述事实的人，选择传播的是什么 ”；“事实本身”是人们永远无法了解的――我认为。一个事实，犹如一段历史，它是多议的，我们只能够截取我们认为有必要传播的加以核实。”<br />
<br />
 <br />
三、事实求是地讲，韩寒并没有反对保卫钓鱼岛。<br />
<br />
不仅不反对，而且他还老道地对保卫钓鱼岛的行为后果做了一番预测：你要“先要看政府的态度，你不能冲在领导的前头”。“弄不好，你还能发现领导和来犯者（指日本人）还在欢快的谈一笔大生意。”<br />
<br />
领导竟与来犯者媾和，分明是死啦死啦地汉奸地干活。韩寒这样虚拟不必保卫钓鱼岛的理由，“深刻”得是否过分？<br />
<br />
读韩文，我还有发现。对钓鱼岛问题是个什么问题，钓鱼岛如何演变成了今天的样子，韩寒并没有兴趣问究竟，哪怕认真地听一听大人议论的兴趣也没有，但是他善于给出令人心惶惶的结论：“我相信我们官方更看重的是自己对内是否稳固，底下石油倒不是那么有所谓，”，“中国政府只要稳定，不要在外交和军事上有任何未知的风险，所以这导致了这个本来不复杂的问题一定将被拖延成一个非常复杂的问题。”<br />
<br />
看得出来，韩寒对他笔下的“官方”，不乏善意为零的猜度，亦不乏直面羞辱的调侃， 这个半大小子在不长的文章里给他笔下的“官方”下了两笔莫须有的黑账：<br />
<br />
一笔，“在我国的版图里，类似有争议有可能引起国家间任何摩擦和局部战乱的地方，只要别太大块，改变了中国是一只鸡的形象，而公众和舆论也不是太了解的犄角旮旯，官方可能觉得让一点也就让一点了，就当卖给地产吓得商了，公鸡母鸡实在是没那么所谓的。<br />
<br />
另一笔，“钓鱼岛因为一直一来比较有名，公众关注度高，尤其是看了多年新闻联播，领导人都是在钓鱼台国宾馆接见外宾，搞半天钓鱼台都归了别人，这太没面子了，所以，钓鱼岛是政府事关领土问题的形象工程”<br />
<br />
文章的最后，不忘强调主题：“不要以为自己真的在急民族所最急，这个民族，总有更急的”。简言之，保钓不急。急，也轮不到你，有人比你急。表面急的人可能会和鬼子媾和。保钓不过是政府的形象工程。<br />
<br />
认真读过韩寒的文章后，我不禁反问一句：说韩寒这个小屁孩儿立场有问题，错在哪里了？<br />
<br />
 <br />
四、从事“反体制宣传”的大人会把孩子带坏的<br />
<br />
坦白地说，我以前没有读过韩寒的任何一篇文章，这一次，除了精读《保住非法字符》一文外，还依照朋友建议，读了“2009南方周末文化原创榜文化论坛”厦门站韩寒的讲演全文。<br />
<br />
看过这篇讲话，我思辰良久一时无语。<br />
韩寒也许真的不是什么立场问题，他还没有到独立研判立场的年纪，面对掌声、女孩的尖叫声，尤其是某些文化人的肉麻吹捧声，韩寒很容易产生“文化不过如此”的优越感。于是，什么文章能反馈回来更大的尖叫声更肉麻的吹捧声，他便拿出什么样的文章面世。就如同他的眼里“文化不过耳耳”一样，他以为那些写的、讲的铩如新的东西都是自己的原创，殊不知，梁子与扣子、立意与笑料，乃为不经意吸纳了品牌三聚氰胺的结果。<br />
<br />
韩寒股份震荡上行K线图表明，有主力机构在努力培养韩寒，他们不惜代价地用一切能够引起韩寒兴趣的东西来试图影响韩寒，韩寒也不负众望，已经走出了主力机构满意的图形，有人迫不及待地将韩寒写入零八宪 章阵营，权作“布莱克小兵领队”，以示生生不息。<br />
<br />
那天，韩寒照着预先不知道什么地方得来的稿子，进行“南方周末主题表演”，貌似很随便地跟在场的人谈起办杂志话题。<br />
<br />
“这个杂志呢很多地方在审查上遇到了很多问题,里面有一副漫画,漫画是一张图,主人公是个男的,他没有穿衣服。当然这就是不可以了,因为相关的法律、法规规定不能露出那个阴部来公开的出版。但这是我认可了,所以我特地把那个杂志特别大的一个LOGO啊就挡在它那个不合法的部位,然后后来这些出版社的审查人员就告诉我说,诶,这个不可以,你把这个人的中间这个地方啊挡住了,你这个是在暗喻&quot;挡中央&quot;。”<br />
<br />
……<br />
<br />
现场笑声大作。<br />
<br />
漫画男裸，内容遭禁；挡住阴部，审查人员斥责暗喻“dang中央”――傻子都知道，这是个有矢量的“匕首段子”。“南方周末原创文化论坛”，竟然就用这样的“原创”来文化，未免太文化了。<br />
<br />
但是，谁能说清楚这仅仅是文化呢？<br />
<br />
场上笑过的男女同学有几人问过，这是谁家的文化？<br />
<br />
小孩子懵懂不谙世事，可以理解；心理反叛期信口雌黄，也可以理解。但是有人借着孩子的生理反叛，做起真真的反叛文章，进行旷日持久的“反体制宣传”，大人会把孩子带坏的。<br />
<br />
韩寒如果真是个聪明的孩子，在掌声中不会陶醉很久。<br />
<br />
