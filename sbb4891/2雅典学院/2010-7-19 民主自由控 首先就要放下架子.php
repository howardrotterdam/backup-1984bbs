													<h2>民主自由控 首先就要放下架子</h2>
						
						
																											<div id="postmessage_749126" class="t_msgfont">民主控也好，自由控也好，除非你只是为了自己爽，否则只要你希望能有更多的人接受你的意识形态，你就要放下架子，具备一种”贩卖意识形态的心态“，换句话说，你是在推销，你是在传播，你是在宣传<br />
<br />
绝大多数的人的思想都是在两个极端中摇摆，越多的人向你那个方向倾斜的越多，你的环境就会越好。<br />
<br />
首先就要认清环境，承认第一土共在意识形态和资源上的强势地位，承认中国传统文化中民主自由价值的缺失，承认民智有限这个现实。认清了环境，承认自己的劣势地位，才能放下架子，摆脱霸道习气，别动不动就摆出一副”你不骂土共你就是傻逼“的嘴脸。<br />
<br />
举个例子，如果一个人说”土共是不好，可现在没土共管着恐怕也不行吧“之类的话，很多人就会很愤怒，因为他眼睛里看到了一个”不像他那么痛恨土共的傻逼“，然后他的精力就会集中在讽刺挖苦谩骂这个所谓的”傻逼“上来 -- 你要知道，像你这么”睿智“”正确“”先知先觉“的人总是少数，动不动说自己的认识是再明白不过的”常识“（哪怕他确实应该是常识）-- 言外之意认识不到这点的人都是傻逼，连常识都不懂，那不就是傻逼么 -- 这种做派，只会让”常识“普及的速度减慢。伽利略哥白尼达尔文认识了一些更接近真理的理论，他们首先要做的，就是小心翼翼的去普及推销他，争取让越来越多的人接受他们的理念，因为他们知道，或许他们比别人更正确，但他们的认识在大环境来说是劣势是弱者，需要小心翼翼的扶持栽培，而不是明明尚未被大多数人所接受，却还要摆出一副”我就是真理，你不信你傻逼“的嘴脸来。<br />
<br />
说白了，这种心态，也是拜我们的传统文化所赐，万般皆下品惟有读书高，以为自己认识到一些别人认识不到的东西，就兴奋了，就觉得自己高人一等了 -- 就和啊Q正传里的假洋鬼子一样。<br />
<br />
换了我，看到一个说”土共是不好，可现在没土共...“这类话的人，我反倒会高兴，在你们眼里又多了一个”不那么恨土共的傻逼“，在我眼里则是”又多了一个知道土共不好的朋友“ -- 什么叫求同存异，首先看到对方意识形态里同自己重合的部分，然后再去关注那些和自己不同的部分，这样你的朋友才会越来越多，你的朋友多了，听得进你的话会被你的意识形态所感染所影响的人也会越来越多；反之，动辄首先看到甚至只盯住别人意识形态中同自己抵触的部分，你的朋友就会越来越少，对你产生反感的人就会越来越多，因对你反感也对你的意识形态和价值观反感的人也跟着增多 -- 你的价值观只会越来越没有市场。<br />
<br />
是因为看了民运们讨伐土共的檄文才更容易对民主自由产发生兴趣？还是因为看了林达娓娓道来的说理文章更容易对民主自由的理念发生兴趣？<br />
<br />
你的形象，就是民主自由意识形态的形象 -- 中国人的文化首先看人，其次看观点，当年好多老百姓喜欢共产党不是因为喜欢共产党的政治主张，而是喜欢共产党亲民廉洁的形象；同样现在老百姓讨厌共产党，也不是讨厌共产党的治国方略，而是讨厌共产党贪污腐败横行霸道的做派 -- 连这点都不明白，还谈什么中国的民主自由？<br />
<br />
共产党再霸道再不好，即便到了今天，共产党时常考虑的就是”影响“，不管他用何种目的来达到他所要起到的”影响“，起码他还在乎”影响“的，在乎影响，就是在乎形象 -- 你都说了，成天抱怨土共洗脑 -- 那我问一句，土共为啥花那么大的精力去洗脑？土共有警察有军队掌握近乎无限的资源，为什么还那么在意国民的集体意识形态？和土共比，你什么都没有，只有一张嘴一个道理，还居然连自己的形象都不顾及，在民众中的影响也不考虑，手里屁也没有就摆出一副高高在上的姿态，都像罗永浩那样 -- 会有一小部分人崇拜你，你自己爽了，你的理念却完蛋了 -- 这有啥用？ 不过是捡了芝麻丢了西瓜的傻逼而已。<br />
