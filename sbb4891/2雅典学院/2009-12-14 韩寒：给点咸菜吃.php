&nbsp; &nbsp; 10月16日21：00左右，袁浦回龙村附近，张某（男，22岁）骑电动自行车与正在路边步行的老人葛某（男，60岁）相撞，老人头部着地，张某自己也受伤。事故后张某逃离现场，当晚又打电话向交警自首，而老人于24日死亡。死因是脑部重伤。 <br />
&nbsp; &nbsp; 交警认定张某承担事故全部责任。 <br />
<br />
事故特点分析 <br />
<br />
1.事故时间多为傍晚或夜间，少部分事故发生在早晨―――傍晚和夜间视线较差； <br />
2.事故地点多在城郊地区道路上―――夜间城郊道路很多无路灯，电动车的灯光又很暗，而骑车人容易放松警惕； <br />
3.事故中死者主要是老人，骑车人普遍为年轻人； <br />
4.死者的死亡原因多是头部着地后脑部受到重创，通常都是几天后死亡―――电动车的力量很大； <br />
5.事故形态大多数是电动车撞上前方同向行走的行人，部分是撞上前方横过马路的行人，只有一起是电动车与电动车正面相撞―――撞上前方行人主要是因为电动车车速快，刹车系统差，通常是骑车人发现后，避让不及。 <br />
<br />
后患无穷――骑电动车撞了人是没保险的 <br />
<br />
&nbsp; &nbsp; 电动自行车骑车人大都是城市普通打工者，收入水平一般，但电动车致人死亡和机动车致人死亡的赔偿标准是一样的，几十万的赔偿对骑车人有时是“不可能完成的任务”。 <br />
&nbsp; &nbsp; 此外，电动车撞人后，被撞行人大都是在医院抢救几天，后抢救无效死亡。因此，除了赔偿死亡后的损失，抢救期间巨额的医药费用也是一笔沉重负担。 <br />
&nbsp; &nbsp; 电动车不像机动车有第三者责任险和交通事故强制保险，目前为止，没有任何一家保险公司接受电动自行车的保险业务，也就是说，发生事故后，不管电动车责任多少，所有赔偿金都需要责任人自己掏钱。对于受害人家属而言，很有可能面临对方无能力赔偿的后果。 <br />
<br />
骑电动车撞人也要追究刑事责任 <br />
<br />
&nbsp; &nbsp; 在事故处理过程中，如果电动车致人死亡或者重伤，而骑车人负事故主要以上责任，就要被追究刑事责任。 <br />
&nbsp; &nbsp; 一般情况下，在经济赔偿上到位的肇事者，法院在量刑时，会酌情考虑从轻处罚，而事故实例中，有很多电动车事故责任人做不到这一点。 <br />
<br />
有些电动自行车速度达60公里/小时 <br />
<br />
&nbsp; &nbsp; 9月27日上午8：25，31岁的四川人王某在笕丁支路撞了一个正在过马路的85岁老人，王某主动到交警大队报案。交警发现王某骑的车功率、重量等全部超标，经鉴定，王某的车属于“电动机动车”，而且这辆车的号牌和行驶证都是假的。 <br />
&nbsp; &nbsp; 王某说，车的牌照是在买车的地方老板给装的，他事先并不知道是假的。 <br />
&nbsp; &nbsp; 目前，市面上很多轻摩化电动自行车，车重、车速都严重超过行业标准和国家制定的可以上牌的标准，有些电动自行车的速度甚至可以达到60公里/小时。 <br />
&nbsp; &nbsp; 交警表示，骑这样的车一旦发生事故，会加大骑车人承担事故责任的比重，即使不发生事故，这些车辆被交警发现，也要扣车。 <br />
