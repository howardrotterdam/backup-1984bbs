													<h2>韩寒：给点咸菜吃</h2>
						
						
																											<div id="postmessage_372701" class="t_msgfont">给点咸菜吃(2009-12-14 12:21:58)<br />
标签：杂谈&nbsp; &nbsp; <br />
<br />
我国的新闻给我的启示是经常让我知道原来我国有各种各样的部门，这次，我就知道了国家标准委员会。国家标准委员会在内部制定了官员一顿饭不许超过十万元，官员一次包二奶不准超过五个，官员一次叫小姐不得超过双飞，官员一次受贿不准超过一亿等标准之后，将视角和触角伸向了民间，要规定电瓶车不准超过15到20公里每小时，重量不得超过40公斤，否则将按照机动车来管理。<br />
<br />
 <br />
<br />
为什么有这个标准的出台呢？很简单，商人是靠出台产品盈利的，政府是靠出台政策盈利的，有了这个标准以后，大部分的电瓶车将要被划为轻便摩托车或者电动摩托车（电动摩托车，这个词汇真新鲜），无论在牌照和税费上都将大大增加。我不是特别理解，相关部门如果是以人民群众的生命安全为幌子的话，那么这一部分超过标准的车，缴纳了更高的费用以后，难道变得更安全了一点吗？难道除了信春哥能不死以外，多交税也能不死吗？<br />
<br />
 <br />
<br />
这次的改革上，我们可以看出相关部门必杀的决心。因为相关部门知道，通过技术手段，狡猾的刁民可以将本来是40公里极速的电瓶车在检验时限速到20公里甚至15公里的国家标准，这一来相关部门钞票就收不到了，怎么办呢？于是就又出台了40公斤的标准，因为跑的比较快的电瓶车难免重量就重，这下你就没辙了。我建议劳苦人民如果电瓶车的确超标了，等标准实行以后，在检验时将占据重量大头的电瓶，轮胎轮圈等拆除，告诉他们，因为信政府，信国家，这人一有信仰，不光不死，而且这电瓶车连没电没轱辘也能开。<br />
<br />
 <br />
<br />
任何的交通都是要付出代价的，政府以安全的名义限制了摩托车很多时间。在其他国家，毕业以后年轻小伙子创业的阶段，一台摩托车是陪伴他青春的记忆，对于很多不那么富裕的家庭，摩托车是出行必须的交通工具，你不能要求每个人必须要去坐公共交通。后来这些原本骑摩托车的人，一部分人选择了地铁和公交，一部分人买了小车，一部分人买了电瓶车。选择地铁和公交的，最近票价都涨了，选择了小汽车的，油价也涨了，在某些奇特的城市还要收牌照费用和道路使用费，但是选择了电瓶车的那些人，也就是这批人中最弱势的那些人，相关部门一直没有能够从他们身上捞到油水，好不容易电价涨了吧，这钱还不归自己。于是，公平公正的原则体现了出来，当年从摩托车上赶下来的那一批人，都要赚到你。<br />
<br />
 <br />
<br />
那么电动车安全么？不安全。因为电动车无声无息，而且刹车性能差，很多电瓶车能开到甚至超过50的速度。但是电动车驾驶员撞死人的事情很少见，更多的是电动车驾驶员被别人撞死。实施新标准以后，对于他们依然被别人撞死似乎没有什么帮助。电动车的标准很简单，应该是牌照登记等手续彻底免费，限制速度不能超过35到40，必须使用碟刹，使用头盔。尤其是必须使用碟刹。我看过市面上很多电瓶车，我看到的大部分能超过50的超级电瓶车，其实所配套的刹车系统和能开到50的轻便摩托车是一样的，都已经主动配备碟刹，这让我很欣慰。这说明这些厂家虽然技术活不见得好到哪里去，基本的良心还是要比一些相关部门要好的，就是你多交钱，服务和配套也会相对应的提升。但电瓶车超过50还是有点危险的，因为轮胎配置比较差，悄然无息，这个速度就可能对行人造成比较大的伤害。<br />
<br />
 <br />
<br />
我有一个邻居，在上海金山石化的某个工厂上班，每天回家要坐公交车25公里回到亭林家中。但是他的公司经常加班，加班了以后他就没有车回家了。他的月薪是1600元，在周围很多的工厂里，这已经算不错的。周围的房价都是1万元。他想买一个摩托车，看中了一台4000多的，要去买。我说我这里有一台一模一样的小摩托车，我开了大概100公里，我1000多元卖给你（请要提问你为什么不直接送给他的脑残朋友多动动脑筋）。一周后，朋友说，我不要你的那台了，虽然便宜，但是还要上牌照的钱，最主要是油钱很高，我一个月要加掉快四五百块钱油了，我承担不起。你看我的电瓶车，也能开到50。<br />
<br />
 <br />
<br />
如果实行了标准以后，我这位朋友的命运就很叵测了，他的选择有两种，一种是交一笔钱，还是开着它的“电动摩托车”，半个小时多到家，如果运气好，可以活到老，另外一种是换一个国家标准的，但每天回家需要开两个小时左右，无论冬雨还是盛夏，而且他的电量还有相当的可能是开不回家的。<br />
<br />
 <br />
<br />
电瓶车是这个城市里倒数第二弱势的人群使用的交通工具，他们往往是疲于奔命的人，你不能让他们以70码的速度去送命，你也不能让他们以15码的速度去奔命。无论如何，我认为相关部门不能再多收他们一分钱了。有些人可以吃鲍鱼，但不能因为看见吃咸菜的人吃太多咸菜而制定一个标准，说你每天必须吃规定量的咸菜，否则太咸了有害身体健康，但解决的办法又不是给人家肉吃，而是将人家已经多吃的或者即将多吃的咸菜按照肉来收费。你以为人家那么乐意吃咸菜吗，你吃一个试试。当然，可能你偶然吃咸菜还觉得挺好吃，就像XX省省长偶然在机动车道里骑自行车上班一次感觉很不错一样。牛逼你就天天吃。</div>

							
							
							
							
