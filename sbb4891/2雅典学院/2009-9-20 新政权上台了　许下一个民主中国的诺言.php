 中共建政 60年：香港故事--文革（ 1966－ 1976）六七暴动地下党员后悔：欠港人一个道歉 <br />
文革极左风潮瞬间染红香江，在 1967年 5月的新蒲岗人造花厂劳资纠纷中全面引爆。中共领导人、《人民日报》推波助澜，左派祭起反英抗暴旗帜，全城遍地真假菠萝、武装冲突喋血街头。军警大肆搜捕「左仔」，香港沦入赤色炼狱。回首前尘，老左慨叹殖民地下爱国有罪，抱怨北京政府欠他们一个说法、也有人醒觉被骗；经此一役，抗暴受伤的警察自认此生不懂爱国，只爱香港。记者：雷子乐　林社炳<br />
