													<h2>港产红卫兵深悔批斗Zhao Ziyang：「被毛泽东骗了」</h2>
						
						
																											<div id="postmessage_255539" class="t_msgfont">生果日报网上版<br />
--------------------------------------------------------------------------------<br />
 中共建政 60年：香港故事--文革（ 1966－ 1976）港产红卫兵深悔批斗Zhao Ziyang：「被毛泽东骗了」 <br />
<br />
那是一场翻天覆地的浩劫。 1966至 1976年，毛泽东发动无产阶级文化大革命，举国上下入了魔，放眼尽是扭曲人性的夺权、批斗。 66年的广州， 10万人钻进越秀山体育场，批斗广东省委第一书记Zhao Ziyang；狂潮里有个香港红卫兵，他是 17岁的罗海星。这小将为毛主席上山下乡，当数不清的无辜者纷纷倒下，他终于嗅到革命糖衣里头有阵阵血腥。记者：雷子乐　林社炳<br />
<br />
1949年中共建政，罗海星在香港出生。父亲罗孚是《大公报》副总编辑、共产党员。根正苗红的罗海星，在培侨毕业后， 65年赴广州升学，不久就碰上这场世纪政治风暴。他当时头脑发热，抱着「讲平等」信念，参加红卫兵。文革的主线是审查干部，罗海星依照干部交代的材料，踏遍东北、延安、新疆、内蒙等地逐一查核，「坐火车全部用公费，国家有乜理由唔穷？」批斗是文革的家常便饭， 66年末的Zhao Ziyang批斗大会，罗海星也身处其中。他当时听不清台上宣读的罪状，只跟随群众高呼「打倒反革命分子Zhao Ziyang，坚决捍卫毛主席。」国家新闻出版署前署长杜导正在《改革历程》一书序言也提到这幕：「造反派逼他（Zhao Ziyang）自己喊打倒三反分子Zhao Ziyang，他拒绝喊。退一步要他喊打倒走资派Zhao Ziyang，他仍拒绝喊，最后他只喊了一句打倒Zhao Ziyang。」文革令政府瘫痪，革命小将透过阅读「两报一刊」（《人民日报》、《解放军报》、《红旗》杂志）掌握毛主席的最高指示去造反。「点做咏裁鳎各师各法。」 67年，毛泽东发动红卫兵上山下乡。罗海星拿起背包火速「为人民服务」。大伙儿想从广东走到井冈山，但来到梅县就因爆发脑膜炎要停步。<br />
<br />
忆书记　自挖深坑受审 放大图片 <br />
 <br />
文革过后，罗海星（右一）淡出红卫兵阵营，赶在毕业前与同学游泰山。<br />
<br />
首次下乡，罗海星体会到农民生活穷困，年入仅得几毫。小伙子当时责备官僚无能，数年后才意识到是制度问题，「平均分配，结果咪一齐穷。」 67年 1月，毛泽东下令要夺权，大家匆匆折返广州。夺权引起派系斗争，文斗变成武斗。广州市的工、农、学生都分旗派、总派，初以水喉通、匕首互殴，个别地区的学生到军营抢枪。批斗也越来越残酷。罗海星忆述，学院一名党委书记，被红卫兵迫令自挖及腰的深坑，要他进坑内受审。老书记后来转由粤北继续审查，家人数年后找到据说是他的坟墓，却没有墓碑，挖出来的头骨有数个小洞，似被利棒拷打至死。文革后他获恢复名誉，「好人都死，仲有乜用？」 <br />
<br />
被煽动　「我O系做错」 放大图片 <br />
 <br />
罗海星（右）父亲罗孚是《大公报》前副总编辑。图为他 16岁时与父母合照。<br />
<br />
年年月月、日日夜夜讲革命，罗海星开始怀疑这场运动。「中央发个文件，半夜三更又仗锘行来行去，有乜意思？」走遍全国审干，到头来发现大部份都是好人；想起批斗时强词夺理，终察觉背后目的是要「整人」！共产党将人分为三、六、九等，也不过要方便管理；各取所需的口号说得漂亮，但三年大镬饭已证明失败。「不过当时唔敢讲，你想死咩？」罗海星慨叹，年轻时实在太「左」，太易被煽动，红卫兵都被毛泽东骗了。「我O系做错，唯一好处系错误中睇到何谓正确，睇到社会矛盾。」 <br />
<br />
年轻时曾深信毛泽东的罗海星，到后来才渐渐醒觉。<br />
黄建通摄</div>

							
							
							
							
