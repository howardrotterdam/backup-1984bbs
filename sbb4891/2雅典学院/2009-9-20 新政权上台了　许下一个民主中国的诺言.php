													<h2>香港故事 三面红旗 （1958－1962）10万饥民涌港 边境山头如炼狱 大跃进掀大逃亡</h2>
						
						
																											<div id="postmessage_253733" class="t_msgfont">生果日报网上版 2009年09月23日<br />
--------------------------------------------------------------------------------<br />
<strong>中共建政60年：香港故事三面红旗 （1958－1962）10万饥民涌港 边境山头如炼狱 大跃进掀大逃亡</strong><br />
<br />
1962年夏天，刘少奇对毛泽东说：「饿死这么多人，历史上要写上你我的，人相食，要上书的。」过了近半世纪，中国官方对 58年至 62年间由大跃进引发的大饥荒，依然含糊其词，香港有媒体至今还是采用「三年自然灾害」的字眼。历史真相，却陆续浮现。1962年初夏，成千上万的饥民翻山越岭，逃到香港，成千上万的港人到山上呼唤亲人的名字。当年直击惨况的香港记者梁林荣坦言：「根本唔系天灾。」那时奉命「一字不报」的左报记者周奕今天更说：「直头系人祸。」记者：陈沛敏、张嘉雯<br />
<br />
1962年 5月，港督柏立基面临严峻考验，上万内地人如波浪般试图越过边境，涌入香港。港府出动英军、警察甚至英国军情五处堵截防范，仍无补于事。香港民情却像初夏的气温一样，越来越热。「梧桐山上惨绝人寰」、「火速！救命！」报纸上白纸黑字的标题，牵动了港人恻隐之心，很多人自发筹款买粮，送到新界边境赈灾。今年 88岁的梁林荣当时是《成报》记者，跑到上水华山采访，「好多人匿埋，等机会入市区，每日有三几千人。有障愀塾星兹耍亲人随山嗌，好彩嗌穸扔腥舜稹何斩龋　痪拖氚旆ù诔鍪星，等机会{身份证。」<br />
<br />
放大图片 <br />
 <br />
<br />
放大图片 <br />
 <br />
1962年，英国著名摄影记者 Larry Burrows在港拍下饥民涌港的照片。<br />
<br />
母死在山上　孩子坐尸旁嚎哭 放大图片 <br />
 <br />
一名新界居民在山头寻亲，叫到声嘶力竭，手持木牌寻人。<br />
<br />
那时一眼看去，满山是衣衫褴褛、皮黄骨瘦的难民。不少人持有内地签发的通行证，但因港府实施入境限额，只好偷渡，内地当局也只眼开只眼闭。有的人已偷渡了六、七次，屡败屡试，「尤私，被警察、喀到，就全部送去粉岭集中营，解返华界。」「O话上面拥檬常好凄凉。有女士见到难民，喊起上。」难民很多来自惠阳淡水，说当地已十室九空，村民都逃向香港。那时的记者，除了采访，还兼职救济，又协助市民寻亲。「有人嗌『阿福』，我O咪帮手嗌『阿福』。有难民写个字条，叫我O带入市区亲人。」部份记者直闯禁区，到边界梧桐山，目睹幕幕人间惨剧：山路险阻，难民走了几天几夜，许多老弱妇孺受伤、病倒，水尽粮绝，坐以待毙；有背着婴儿投奔自由的母亲不支倒地，死在山上，几个月大的孩子坐在尸体旁嚎哭，却无人伸出援手。 <br />
<br />
《文汇》前记者：只字不提　好蠢 放大图片 <br />
 <br />
《文汇报》前记者周奕。<br />
<br />
超过 10万人就这样在那年夏天偷渡来港。不少报章天天头版报道，又组织救援队，甚至有报馆派车送面包到边境，报纸也销量大升。但同一时间，本港左派报章对「大逃亡」却只字不提。当时任职《文汇报》的周奕说，上级下令「一字不报」，「当时好蠢，踊昂盟&#60811;家人讲话要独立思考。」他也到过梧桐山，跟随广东省委工作组实地调查，写报告作「内参」。那究竟这场饥荒是天灾还是人祸呢？周奕说：「直头系人祸。被骗几十年，前几年先睇到资料话，当年大陆饿死 3,000多万人。」 <br />
<br />
新华社前记者 着《墓碑》谈教训 <br />
父亲在大饥荒时活活饿死的新华社前记者杨继绳经实地采访和研究，去年在港推出《墓碑》一书，指出大饥荒是毛泽东专制下「三面红旗」造成的惨剧，也间接引发另一场浩劫──文化大革命。「没有前 30年的教训，就没有后 30年的改革开放。对于大饥荒那段历史，研究才刚开始，还没有总结够。」香港成为同类书籍的一扇窗口；他估计，再过 10年或者 20年，《墓碑》才可望在国内出版。<br />
<br />
他这样说 <br />
「没有呼天抢地的哭声，没有披麻带孝的礼仪，几千万人就这样无声无息地消失了。」－－－新华社前记者杨继绳著作《墓碑》<br />
<br />
领导人语 <br />
「你们用 10年时间超过美国，我们也可以用 15年时间，赶上或超过英国。」－－－ 1957年 11月毛泽东在莫斯科会议上针对前苏联的讲话 <br />
<br />
<br />
梁林荣珍藏大量 62年大逃亡的剪报，包括当年有报馆送面包赈灾的照片。</div>

							
							
							
							
