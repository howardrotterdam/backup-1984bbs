「收猪肉喇！」六十年代邮差口中的「猪肉」，其实是港人寄给内地亲友的布包裹，内里的炼奶和「阿婆香水」，是内地同胞眼中的宝。物资匮乏的日子，他们还会将布袋用来做衣服、面巾。58岁的叶满光现职高级邮差，处理过不少港人自制的布袋包裹，每个不可超过两磅，邮费$1.6，很多人会放两罐炼奶，「有人博唔磅，摆多支药油落去。」内地邮政规定，包裹只可索绳，不可封口，每人每月只可收一次，啡色布袋因貌似猪肉而得名，「（包裹）多到一日都寄唔晒，要租个仓贮存住先，尖沙嘴半岛旁边有间消防局，租埋做货仓。同事话袷比颂多，做到&#62546;晒S。」「猪肉」多寄往福建、厦门及潮州，当时内地来港的民聚居北角、上环、深水叮附近的邮局特别忙。<br />
<br />
缝白毛巾写地址 <br />
叶满光也替妈妈寄过眉豆、罐头和药油，「妈妈好有心机，系罘欤缝好之后再反转，上面再缝张白毛巾写名写地址，对方收到（毛巾及布）可以用得番。」「当时香港都好困难，我记得阿妈 55、 56年返乡下，就算忧都会担饭焦、面包皮返去，系血浓于水裰帧！鼓诘厍兹擞谩改戏纭估葱稳菹愀劾捶玫那子眩代表有物资接济，「阿妈背住细佬，又过渡轮，又再转车，一早去，夜晚先到，背住支担挑唔敢b，惊俾人偷。」后来国家开放，他随家人返乡，「带衫返去要打税，着丈砭瓦硎勾颍所以个个着几条裤、几件衫，流晒大汗，过关詹匏换。表弟叫我带录音带畀冢袷贝 100%税。&#60811;家就有钱喇，返去请我食饭，一餐饭几千蚊，食野味、食鲍鱼，晏昼都开白兰地。」 <br />
<br />
<br />
叶满光说大饥荒时港人自制的寄包裹啡色布袋，又名「猪肉」。韦志满摄</div>

							
							
							
							
