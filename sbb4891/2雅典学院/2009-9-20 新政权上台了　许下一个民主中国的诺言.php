<br />
近期屈记者藏毒煽动 <br />
至于其它传媒，程翔表示，June Fourth前中共虽一贯地控制新闻，但仍有自信，「June Fourth后，中共乜S都怕人知道，越来越怕记者。近期仲屈记者藏毒、煽动，仲衰过以前。」 <br />
