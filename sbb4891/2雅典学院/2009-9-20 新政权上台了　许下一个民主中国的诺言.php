													<h2>中共建政 60年：香港故事韩战篇（ 1950－ 1953）突破韩战禁运</h2>
						
						
																											<div id="postmessage_250842" class="t_msgfont">生果日报网上版 2009年09月21日<br />
--------------------------------------------------------------------------------<br />
何铭思忆述　霍英东被水警一脚踢晕 <br />
<br />
韩战爆发，港英殖民地政府加入对中国大陆实施禁运的行列，铜铁、药物、纺织品等物资一律严禁运往内地，中港之间正常贸易几近停顿，却造就了拥有船队的霍英东、曹文锦等港商，经营偷运物资生意致富。霍英东生前好友何铭思忆述，后来成为全国政协副主席的霍老当年也曾疏通警察，又亲自出海探路，「谑怨俾水警一脚踢晕，呢生意唔易做。」不管是为赚钱还是为爱国，香港当时确是中共抗美援朝的重要补给站。记者：张文杰、林社炳、陈沛敏<br />
<br />
「凡是一个士兵可以利用的东西不许运往共产党的中国。」 1950年 12月美国宣布对华实施禁运，翌年英国立即跟随，拥有至少六、七艘船的霍英东知道，这是一个机会。霍老生前引为知己的何铭思，今年已届 87岁，接受本报访问时却清楚记得，霍英东向他讲述当年突破禁运的事迹，「诨白畛踔幌迪胱錾意，后来先明白系乜S一回事，觉得系一件中国人要做事。」霍英东是水上人出身，战后购入几艘货船，开展航运生意。韩战爆发后， 1951年初中共控制的华南企业在港搜购了大量战略物资，包括锌片、铁片、橡胶和盘尼西林等，要找法子运到内地。曾任新华社香港分社统战部长的何铭思指出，那时霍英东被当局认定是偷运物资的理想人选，他只须负责运输，运到深圳蛇口落货，有时就经澳门运往内地其它口岸。<br />
<br />
何铭思形容，霍英东行事细心，事事亲力亲为，为确保行动顺利，亲自「上警车讲数」，给钱疏通，取得当局缉私艇的行动消息。当时，霍英东住在六国饭店，晚上在窗前用电筒发讯号，指挥货船何时出海，「谙邓上人家，知道风会唔会来。」有时他更会亲自出马，先驾驶没有载货的船只出海探路，但有次遇上水警巡查，「水警上船查唔到S，个番鬼佬帮办嬲起上，一脚踢落诙龋成个晕落地，谑哟宋耻辱。」偷运物资始终存在风险，霍英东旗下的「黑猫号」就在偷运物资时遭水警和缉私船只撞沉，「谇卓谕我讲过，话呢门生意唔容易做，相比起卖楼花，唔算系好到钱。」话虽如此，经过三年的「走私」生涯， 1953年霍英东已可拿出 280万元，购入利希慎家族位于铜锣湾的使馆大厦。霍英东转为投资地产，更首创楼花制度，身家暴涨。不过，韩战时他偷运物资到内地，却埋下了他与港英交恶的伏线。前总理周恩来曾形容霍老等在抗美援朝期间为内地运输物资、「冲破美帝封锁」的港澳人士是「患难之交」，霍老后来更跻身国家领导人行列，逝世时新华社说他是中共的「亲密朋友」。 <br />
<br />
相比霍英东，曹文锦却不讳言，当时是为势所逼才做偷运这种买卖，「禁运下无航运生意可做，运送禁运物资是唯一出路。」曾被称为香港四大船王之一的曹文锦指出，当年其美元户口被冻结，需以瑞士法郎在伦敦购买药物，经由属于共产主义阵营的波兰运到香港，再分拆逐小份经无参与禁运的澳门运到内地，「那时没有想甚么，只是想生存。」可是，人民币当时在内地以外地区无法兑换，曹文锦惟有用以物易物方式交易，以药物等物资换取内地土产，例如黄豆、绿豆、桐油，再转运日本，换取战后剩余的钢材及化工物品。偷运过程不时发生意外，当时台湾协助美国实施禁运，在沿海布下水雷封锁海域，曹文锦的一艘货船就因碰到水雷被炸沉，货物被沿岸的内地人抢光。香港中华总商会前会长王宽诚在韩战期间，则以「维大号」名义捐出一架战斗机支援中共志愿军，带头认购人民胜利公债，有指他在发行首天就认购了 36,000份公债，成为海外之冠。</div>

							
							
							
							
