<br />
建政 60年「仲有乜庆祝？」 <br />
邓道仪也不好过。她的父母是香港人，红卫兵指她是特务，送往放牛劳改。当时女儿 12岁、儿子 10岁，无父母照顾，女儿患上肾病， 18岁时无药可救，医院指她父母是「牛鬼蛇神」拒绝施救。女儿临终前求妈妈送她到香港诊治，但已太迟。「谧裾笸蝗宦浯笥辏我觉得系个天对我、对文革一种惩罚。」共产党日夜洗脑，令无辜者也自觉活该。「当时觉得自己应该被斗，怪自己出身唔好。」邓道仪说，直到 79年来港才懂反思。「香港重视个人价值，袷毕戎道文革好专制，好唔！」建政 60年歌舞升平，一对老夫妇没有同感。邓道仪说：「我O都俾你害得畈遥仲有乜庆祝呀？应该讲多历史，好坏都讲，畀年轻人警惕！」 <br />
