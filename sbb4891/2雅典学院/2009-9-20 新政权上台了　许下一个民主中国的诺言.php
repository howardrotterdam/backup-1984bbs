																											<div id="postmessage_264416" class="t_msgfont">生果日报网上版 2009.9.30<br />
--------------------------------------------------------------------------------<br />
&nbsp; &nbsp;<br />
新华社香港分社的重要任务是统战香港各界人士，有民主派人士透露，早于七十年代新华社已开始积极与香港学界、社运人士接触，到八十年代香港前途谈判，新华社更将统战范围扩展至民主派，可是随着民主派争取 1988年直选，及后再发生June Fourth屠城，北京政策全面左转，新华社及回归后的中联办已全面与民主派划清界线。<br />
<br />
八十年代 对港政策开明 <br />
民主党主席何俊仁早在香港大学搞学生运动年代，已跟新华社香港分社「交手」。何俊仁回忆说，民主派人士与新华社交往可以分成三个阶段。第一个阶段是「四人帮」倒台前，新华社经常派人参与国粹派的学生运动，对他们这些不认同四人帮做法的学生，则完全拒绝接触， 1976年 5月何俊仁曾带队到新华社抗议中共镇压四五天安门事件，与新华社保安冲突。第二阶段则是中英八十年代展开香港前途谈判，由于民主派支持「香港民主回归」，北京为抗衡港英政府，开始接触民主派人士。何俊仁说当年新华社香港分社副社长乔宗淮常与他见面。 85年获委任为基本法起草委员的支联会司徒华也说，当年曾多次与前新华社香港分社社长许家屯秘密见面，第一次是 87年，许家屯相约他与另一位草委李柱铭于赤柱别墅会面，建议他俩组党，反映当年北京对港政策相对开明，愿意与民主派保持沟通。然而，随着 89年June Fourth屠城，民主派反对北京血腥镇压民运，民主派与北京及新华社关系急转直下。司徒华回忆，他与李柱铭宣布退出草委会，许家屯得悉李柱铭说考虑重任草委，实时以专车接载他俩再到赤柱别墅，抛下一句「你O两个唔可以返番去草委会」，司徒华「觉得好戆居；我都完全右馑挤等肴ァ埂Ｃ裰髋捎胄禄社沟通也开始进入第三阶段，双方再次不相往来。何俊仁坦言，当年新华社与民主派沟通只是北京的统战手段，利用民主派支持回归抗衡港英政府，所以当民主派争取 88年直选、要求香港回归有民主普选，双方关系即转趋紧张， 89年后更全面左转，直到今天，中联办仍未改变左倾政策，完全拒绝与民主派来往，除非北京改变对民主派政策，否则双方不可能重建正常关系。<br />
