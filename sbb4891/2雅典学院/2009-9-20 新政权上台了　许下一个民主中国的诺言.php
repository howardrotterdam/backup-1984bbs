镇压工潮．以血还血 <br />
「呢个（六七暴动）系北京欠香港爱国同胞债！」今年 76岁的《文汇报》前记者周奕，曾贴身采访暴动、工潮，见证北京思潮如何影响香港左派。 67年 4月，新蒲岗香港人造花厂资方宣布削减奖金、津贴，解雇谈判工人。 5月 11日防暴警察发射催泪弹、木弹镇压， 13岁理发店学徒陈广生被木弹击毙，掀起暴动序幕。5月 16日， 101人成立「港九各界同胞反对港英迫害斗争委员会」（简称斗委会），工联会理事长杨光（图）出任主任。周奕忆述， 6月 3日《人民日报》社论指示香港同胞将斗争矛头指向英帝国主义；月底周恩来说：「中国人民支持香港同胞取得最后胜利。」 7月 5日《人民日报》社论呼吁香港同胞「对英国殖民主义者……杀人要偿命」，令反英情绪不断升温。「放炸弹，系《人民日报》社论叫，话要以血还血。&#60811;家认唔认呢笔账先？」<br />
