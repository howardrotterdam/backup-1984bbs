生果日报网上版 2009年09月22日<br />
<br />
如果十月一日还有值得庆祝的余地，陈友道会庆幸当年身为「黑五类」却获得从轻发落，劳动教养短短四年便摘下帽子，没有饭吃还有番薯叶，不能与妻儿同住还可以每星期见面一次。比起死的、伤的、疯了的「右派分子」，他自觉很好运，真的很好运。记者：蔡元贵<br />
<br />
陈友道现年 79岁， 1958年被打成右派。当时县委书记号召数千名小学校长与教师开会，说是帮助党整风：「其实小学教师对上级政策知道好少，我O咏驳截S意见，何况我对毛主席好崇拜、好尊敬。」最后身为小学校长的陈友道只说了一句批评上级的话，认为教育局长「不出门能知天下事」的官僚作风应该改变，就是这句话，改变了他的一生。<br />
<br />
说上级不是「轻轻一句变右派」 <br />
「裎唤逃局长当时已经下台，我轻轻批评一句，都被视为反党、反社会主义。O话局长代表党，你反对诰拖捣炊缘常我无话可说，就畋涑捎遗桑好好笑。」参加整风会议的教师，其后有数十人被打成右派：「有个同学知道我被打成右派，惊自己都有事，跳水自杀。」陈友道遭受的惩处是褫夺校长职位，并被下令回乡，名义是担任生产监督，实际就是返乡下耕田，历时四年。他说自己算好运，很多知识分子被判劳动教养二十年，送到远离家乡的地方，与家人隔绝，死、伤、疯了的都有。<br />
<br />
校长去耕田「差连泥都要食」 <br />
上山耕田的日子很苦，每天工作起码八小时，没有收入，从前当校长的 50元月入失去了，如今只能与公社一起吃大镬饭，每人只分得很少很少食粮：「我O要食地瓜叶（番薯叶），总之见到乜都食，差连泥都要食埋。有个同乡爷爷，当年就疃鏊。」1962年，陈友道终于获得「摘帽」，他说：「我都唔清楚点解，总之话我表现好，可以重新安排工作畀我。」陈友道重获自由，即向单位申请移居香港，但不获批准。他惟有继续留在福建当教师，但依然不能与妻儿同住，每星期只能团聚一次。至 1980年，经过无数次申请，陈友道获批来港，然后逐步申请妻子与儿女来港。来港后，陈友道只能从事劳动工作，一天做五份工，在理发店与图书馆等不同地方做清洁，朝七晚九。坚持离乡背井，为了当个清洁工？陈友道解释：「我谂住香港有自由平等、有民主，大陆乜都唔得。」更切身的原因是：「几十年来，人O都用歧视眼光睇你，我唔敢同人倾偈，唔敢同人坐埋一齐，自卑感好大。」 <br />
<br />
家人受歧视「大女升唔到大学」 <br />
摘帽不等于重见天日，自从陈友道被扣上右派帽子那一天起，他和家人都遭受社会歧视：「我大女想升读大学，申请『三好生』唔批，升唔到大学，就系因为家庭背景唔好，系黑五类子女。」中共建政 60年，当年以言入罪的陈友道感触良多：「共产党点解会睿咳嗣窳出声都唔敢。畹愕茫亢盟屏跸波睿为人民出声，唔系为自己升官发财，点解都要捉？虽然&#60811;家经济好，但系幼杂用裰鳎好似一座有饱饭食监狱一样。」 <br />
