&nbsp;&nbsp;<br />
中共出动人民解放军屠杀手无寸铁的人民，当年连本港的老左派也气愤难平，纷纷谴责。June Fourth后富商霍英东与十多名港澳人士发表联合声明，公开谴责北京屠城，哀悼死难者；霍老挚友、时任新华社香港分社副秘书长何铭思更登报脱离共产党，他说至今无悔，「自己人开枪打自己人，我唔能够接受。」上世纪四十年代建政前已加入共产党的何铭思，入党时才 20岁出头，自言甚么都不懂，不知何谓共产主义，只见当年共产党员「真系热心救国，真系爱国家」。June Fourth血腥镇压，他认为反映中共完全背弃了过去宣示的宗旨。<br />
