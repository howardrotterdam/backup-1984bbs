													<h2>历史解读　邓小平第三度复出　中国向世界开放</h2>
						
						
																											<div id="postmessage_259801" class="t_msgfont">生果日报网上版<br />
--------------------------------------------------------------------------------<br />
 <br />
改革开放是中共在 1978年十一届三中全会上提出的一条「对内改革、对外开放」的战略决策，是中共建政以来首个对外开放的基本国策。对内，它大幅提高了人民的生活水平，让中国进入了经济迅速发展时期；对外，它改变了中国长期以来自我封闭的状态，令中国向世界开放。但由于改革开放的过程中，中共高干子女参与倒买倒卖商品，从中牟取利益，损害公众利益等贪腐行为，成为 1989年June Fourth事件的导火线之一。1976年文革结束后，中国经济处于崩溃边缘，人民生活贫穷、工农业技术落后，国家财政赤字严重。与此同时，中共亦面临执政危机和信任危机。但随邓小平在 1978年 12月 18日至 22日召开的十一届三中全会第三度复出，并确立了他最高领导人的地位，中国的政策产生了历史性的剧变：由毛泽东时代进入了邓小平时代；不再以阶级斗争为政策中心而是转移到经济建设上；冲破计划经济的枷锁，逐步建设和发展社会主义市场经济的体制。<br />
<br />
「大包干」改革的起点 放大图片 <br />
 <br />
在改革开放下，深圳特区的经济取得迅速发展。<br />
<br />
数据显示， 1978年国内生产总值（ GDP）总量为 3,645.2亿元（人民币．下同），国内人均生产总值为 381元。其中，城镇居民可支配收入 343元，农民人均纯收入仅 134元。当时，农村绝对贫困人口，即人均年收入低于 100元者，多达 2.5亿人。由于邓小平提出「要允许一部份地区、一部份企业、一部份工人农民，由于辛勤努力成绩大而收入先多一些，生活先好起来。」同年 12月底，安徽省凤阳小岗村 18户农民，为解决温饱问题，秘密签定契约，将属于国家的集体耕地包产到户（俗称「大包干」），成为中国改革的起点。大包干打破了旧有吃大镬饭的体制。它虽然保留集体组织的形式，但土地经营权完全掌握在农民个人手中，他们可自行安排生产，除国家税捐外，收获全部归己。农民的收入因此大大提升，部份农民更跃升为万元户（年收入逾万元）。为进一步推动改革和开放， 1979年 7月 15日，中央决定在深圳、珠海、汕头和厦门试办特区。参考亚洲四小龙（新加坡、香港、台湾和南韩）的发展经验，将加工装配、合资经营作为主要的外贸形式，并许可中外合资企业存在，意图从中学习国外领先的科学技术和优秀的经营方式。 1984年，又进一步开放了广州、上海等 14个作为沿海开放城市，并将环渤海地区、长江、珠江、厦漳泉三角洲地区辟为经济开放区。 <br />
<br />
高干子弟　抢闸暴富 <br />
邓小平提出的改革开放政策，令中国经济取得迅速发展的同时，以邓家子女为首的中共高干子弟纷纷下海经商，抢闸暴富，利用权力从价格「双轨制」等政策中牟取利益，引起民怨沸腾。当时，中国的物品价格除了按供求关系调整的市场价格外，部份重要物资，如雪柜等电器，会用低于市场价格提供给指定企业，称之为计划供应。同一种物资，市场与计划之间的差价十分大。于是许多高干子女利用特权，占有计划物资转到市场高价出售，从中取利，损害公众利益，被称为「官倒」，当中邓小平长子邓朴方、Zhao Ziyang儿子赵大军和LiPeng儿子李小勇等被称作中国最大的官倒。 1989年，学生提出「反官倒、反腐败」，邓朴方的康华公司等首当其冲，埋下June Fourth事件其中一条伏线。中国组<br />
<br />
当年口号 放大图片 <br />
 <br />
<br />
「手术刀不如杀猪刀，造导弹不如卖茶叶蛋。」－－－在改革开放的年代里，民间已意识到知识贬值 <br />
<br />
领导人语 放大图片 <br />
