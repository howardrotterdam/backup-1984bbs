													<h2>June Fourth劫后余生　学联左仔醒觉了</h2>
						
						
																											<div id="postmessage_261786" class="t_msgfont">生果日报网上版<br />
--------------------------------------------------------------------------------<br />
 中共建政 60年：香港故事June Fourth屠城（ 1989）June Fourth劫后余生　学联左仔醒觉了 <br />
1989年June Fourth屠城，令香港人认清中共的残暴。这一场血腥镇压，对当时正值青壮年的一代香港人影响尤其深远。他们当中，有的人曾对回归抱有理想，不少人曾对改革怀有希望。但枪声一响，一切顷刻粉碎。June Fourth当晚一直留守天安门广场直至清场的中大毕业生黎洪，曾经狂热爱国，平安回港后骤然醒觉，不再盲目拥护共产党。曾上京声援学运的陶君行自言曾是学联中的「左仔」，June Fourth后变成主张结束一党专政、要求平反June Fourth的支联会常委，从此踏上从政之路。记者：张文杰<br />
<br />
在中文大学读书时是国是学会成员的黎洪自言，八九前他有点「盲目」爱国，每年与志同道合的同学随交流团往大陆；那时他以为，八十年代内地经济上的改革开放，会慢慢伸延到政治方面。八九学运爆发后，他参加完数学系的毕业试就立即赴京，以港人捐款购买油印机及扬声器等物资，支持学运，「其实预会镇压，谂住最多俾人抬走，于殴钛腥。」<br />
<br />
「原来畈斜」不再盲目拥共产党 放大图片 <br />
 <br />
黎洪不再盲目拥共，并相信当LiPeng等人离世后，June Fourth必可平反。谢荣耀摄<br />
<br />
89年 6月 3日晚，天越黑，枪声越频密，无任何通讯工具下，黎洪不清楚广场外实际伤亡数字，只感到枪声渐近，不断有板车载着中枪的伤者经过。「手、脚、身体中枪，成身血，不远处仲见到流弹飞过。」他跟几千学生留守人民英雄纪念碑附近，凌晨 5时，纪念碑已被进入天安门广场的军队包围。当时他听说，刘晓波等「四君子」与军方谈判后，军队同意放最后留守广场的学生一条生路，「柴玲叫大家走，有人唔肯，十几个迷彩服士兵走近，向纪念碑上方，即系我头顶几湛枪，逼我O撤走。」June Fourth屠城后，黎洪跟很多本来爱国的年轻人一样，对中共彻底失望，认清政权的真面目，「原来可以畈斜，我唔再盲目拥护共产党。」劫后余生，至今黎洪忆述旧事仍然激动，「好愤怒，觉得（中共）好[线。」回港后他回归平淡， 92年到香港华仁书院任职数学教师至今，近年担任公民教育主任的他，心中那团火仍未熄灭。「 20年，始终有S放唔低，听 Beyond《抗战二十年》谂番好多，好触动我。」近期再与经历June Fourth的朋友联络，更想积极将这段历史告诉学生。「（June Fourth）始终占我生命中好重要位置，不可磨灭，始终有个结，不可以原谅（中共）。」他深信，当LiPeng等人离世，June Fourth一定可以平反。「一日June Fourth未平反，国庆都只系国殇。」陶君行八九时是香港学联秘书长，当年学联相当「亲中」，每有新委员上任新华社都会派人祝贺，「我O反对殖民地统治，主张民族回归、民主建港，属于国粹派。」那时火红年代已是过去式，学联的路线是「放关建争」──放眼世界、关心祖国、建设民主香港、争取同学响应。 <br />
<br />
价值观改变弃商从政争取民主 <br />
陶君行当时已认为，香港要有民主，必先争取中国民主。 89年 4月他北上了解学运情况，与几名学生代表抵京后，与吾尔开希等学生领袖会面，《人民日报》「 4.26社论」后陶君行回港做后援工作，那趟是他第一次也是最后一次上京。June Fourth令学联与中央决裂，学联提出与中方「不承认、不接触、不妥协」的「三不政策」。新华社批评陶君行太激进，更指他本质是反共。陶君行醒觉中共独裁，也根本无法实现社会主义的均富，更担心 97年不是他心目中的民主回归。为争取中国及香港民主，他毕业后在商界打了一年工，便放弃月薪 12,000元的工作， 91年参选区议员。陶君行坦言，June Fourth改变了他的价值观，「人O（内地民运人士）条命都，我又点呢？放弃做商界，赚少钱，算得系咩。」他说，若没有June Fourth，他八成不会从政。当时的学联成员至今仍从政的，还有张贤登及蔡耀昌，都是泛民一员，其它成员不再涉猎政治，但陶君行坚信，June Fourth在他们这代年届四十的人心目中，不可磨灭。那一年，香港百万人上街，民主歌声献中华、支联会成立并以平反June Fourth、结束一党专政为终极任务，加上拯救民运人士的黄雀行动，香港被中共定性为反共基地，以致后来对《基本法》的制订变得强硬，更在 23条重新加入颠覆国家罪，对回归后的香港影响深远。<br />
<br />
放大图片 <br />
 <br />
陶君行说，若June Fourth事件没有发生，他也许不会踏上从政之路。黄建通摄<br />
<br />
放大图片 <br />
 <br />
1989年 4月，陶君行（左）在天安门广场参加追悼Hu Yaobang集会。这是他第一次也是最后一次上京。<br />
<br />
<br />
<br />
<br />
1989年香港百万人上街，此后，港人争取平反June Fourth的决心从来未变。</div>

							
							
							
							
