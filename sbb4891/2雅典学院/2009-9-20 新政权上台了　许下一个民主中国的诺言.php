													<h2>中共建政60年：香港故事在专权政治边缘60年</h2>
						
						
																											<div id="postmessage_250347" class="t_msgfont">生果日报网上版<br />
--------------------------------------------------------------------------------<br />
前言60年前，是中国现代史上最重大的转折。经历抗日战争，经历国共内战，苦难的土地上再次改朝换代，彻底改变了我们上一代和我们这一代多少中国人的命运。历史不应只由掌权的说了算，在中国共产党成立中华人民共和国整整一甲子之际，本报今天起推出《中共建政60年：香港故事》专版，从香港的视点回顾过去，慎思未来。<br />
<br />
北京倾全国之力庆祝建国60周年。电影《建国大业》与种种庆祝国庆的活动如火如荼。可是，如果我们问：1949年是中国建国吗？那么中国岂非只有60年历史，又如何是五千年古老大国呢？回顾历史，1949并非中国立国之年，也不是中国亡国后又重新建国之年，而是同历史上的多次变迁一样，从另一批中国掌权者手上夺得政权的改朝换代。准确地说，49年不是建国，而是中共建政，今年是中共建政60年。<br />
<br />
正名<br />
<br />
改朝换代的是大陆，被代替的政权则迁移台湾。因此，今年也是国民党政权迁台的60周年。大陆固然有60年的庆祝活动，台湾书市也出现了1949年的热潮，许多作家纷纷出版对国府迁台60年的政治、社会或个人的回忆。49年对台湾也是一个重大的转折。夹在两岸中间的香港，在英国殖民统治之下，49年的政治是稳定的。49年前后，突然涌来了许多移民，使1945年只有60万的香港人口激增。那时，香港人把所有不讲粤语的外省人，通称为「上海佬」。上海佬把外省经营工商业的资金、管理、文化，带来了香港，香港经济开始了跃动。60年的香港故事，就从全国各省迁来大批移民开始。<br />
<br />
过客<br />
<br />
在两岸风云变幻的动荡中，来港的合法非法移民越来越多。六十年代的逃亡潮涌进大批人口，成为香港居民的主流。来港的移民与香港原居民一起，在政治平静和法治稳定之下，过着中国人民几千年来梦寐以求的安居乐业日子。港英提供的法律，使这里的中国人能在基本上机会平等之下，得以各显神通，从而创造出香港的经济奇迹。大陆的政治运动与社会主义均贫化对香港人没有实在的冲击，影响最大是心理上使香港人一直存有过客心态，认为他们在香港的福气，是「借来的地方、借来的时间」的非永久的福气。香港是一个跳板，许多人总想着藉此跳出海外，远离中国。他们从没想过植根香港，归属香港，以香港作永远的家。直到70年代中期，文革及67年暴动居然没有动摇香港，港英政府的廉政、公屋及保障社会公平的措施，使香港市民逐渐滋生出归属感。《狮子山下》及许多以香港屋檐下为题材的电视剧，香港粤语流行曲的兴起，使香港人有了自己对香港的认同。这种心态的转变，就成为日后港人要求发展民主的意识形态基础。<br />
<br />
迁徙<br />
<br />
随后九七问题出现了，中国收回香港的意向冲击着港人刚刚建立起来的对香港的认同，人们认识到我们还是无法摆脱中国大陆政治的阴影。一百多年在中国政治动荡的边缘得以幸免的香港，终于尝到1949年中国专权政治的滋味了。港人对八九民运的投入，「June Fourth」屏幕前的热泪，及其后北京对「June Fourth」的取态，冲击着港人的价值观。九七和June Fourth，使香港出现了移民潮。对于许多香港人来说，这是二次逃亡。第一次从大陆逃来香港，第二次从香港逃往美加澳纽。接下来是香港回归。回归后司法独立所提供的人身安全和自由的保障，稳住了香港人心。随着中国大陆改革开放带来的经济崛起，又使许多在海外无法适应移民生活的港人，再回流香港。对许多人来说，这是第三次迁徙。人生有多少个十年？人生经得起多少次飘荡、流徙？香港的故事就是在中共建政60年的冲击下，躲避、创业、迁徙、拼搏的辛酸和没有根的故事。60年来，香港人的国家民族意识经历很大的改变。80年代中英谈判前，香港所有中文报纸，除了中共办的左报之外，大都以「中华民国」纪年，五、六十年代香港的舆论，政治思潮，学术文化，是国共思想斗争的延伸。舆论非左即右，阵线分明。只有少数报刊有独立舆论。但在中共眼中，独立舆论也被贴上「亲英」的标签。回归后，一切翻转。所有报刊都以公元纪年，大部份舆论，已自动献身于「爱国」了。<br />
<br />
人生<br />
<br />
笔者1948年5月跟从父母，从动乱的大陆移居香港。逾60年来，离港往外地旅行居住的时间，从未超过三个月。笔者的成长、工作、生活、婚姻，也都受中共政权的影响。中共建政后不到一年，笔者就进入左派学校即爱国学校就读，毕业后在左派出版社任职，后创办左派刊物《七十年代》开始论政生涯。五、六十年来，关心国是，所从事的职业也紧系着国家民族的命运。要说「爱国」的资格，也够老了。五十年代香港的「爱国力量」仍薄弱，但绝大部份的爱国左派都怀着纯真的理想，认同中共宣示的价值观，要为祖国的事业献身。先是一批批中学毕业生回内地升学，其后在大跃进期间又有大批工人被动员回内地贡献自己的力量。但这些献身祖国的人，都经历了「我爱祖国，但祖国爱我吗？」的歧视，匍匐于「海外关系」的阴影下，在大陆是二等公民。他们怀着身心的创伤，灰头土脸地一个一个在十年后又陆续重回香港，几乎无一例外。回港后大部份人缺乏适应香港社会的技能，落寞地生活在底层。这里多的是有血有泪的香港故事。<br />
<br />
爱国<br />
<br />
至于留在香港、投身爱国机构的怀抱理想的年轻人，包括左派影圈中曾红极一时的影星，大部份也都晚景凄凉。宣称「为人民服务」的爱国机构，对属下员工比绝大部份的资本家更无情。这里也多的是悲戚的香港故事。回归后，香港故事改头换面。原来不爱国甚至反爱国的，都爱国了。爱国不再是纯真情怀，而是政治经济利益。于是，那些早年抱着追求国家均富理想的老一辈，在现实面前，对中共「爱国不分先后」的政策作出感叹：先爱国不如后爱国，后爱国不如不爱国。中共政权的本质也变了。掌权者除了继续紧抱专政权力不放之外，建政初期「为人民服务」的理想，也变成视老百姓为「屁民」的既得利益者。「革命革了几十年，一下子回到解放前」。不，老百姓的地位应比解放前更不如。向钱看、向权看是泛滥全国的价值观，它也像传染病似的毒化香港原来机会尚算平等的社会。香港衍生出种种让人想象不到的奇形怪状故事。中共建政60年，太多是非颠倒、黑白混淆。清代孔尚任的曲词：「眼看他起高楼，眼看他宴宾客，眼看他楼塌了」，正反映我们这一代曾追随中共建政时的理想号召的过来人，今天的心情：理想的高楼已崩塌，换上豪华的、朱门酒肉臭的高楼；宾客还继续饮宴，但所有宾客都不是昨日面貌了。这座新型大厦的桩脚稳固吗？还是终有塌楼的一天？</div>

							
							
							
							
