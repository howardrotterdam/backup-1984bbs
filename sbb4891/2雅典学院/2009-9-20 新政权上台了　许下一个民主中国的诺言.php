June Fourth事件的直接导火线是中共前总书记Hu Yaobang逝世。胡任总书记时，因在党内提倡开放，反对压制民主而遭党内元老围剿，被迫在 1987年下台，由Zhao Ziyang接任；但胡的胆识获社会各界高度赞扬。 1989年 4月 15日，他在郁郁不欢中突发心脏病去世，引起社会极大回响，京城大学生们聚集天安门广场，悼念Hu Yaobang。悼念Hu Yaobang活动迅速演变为大规模集会，演变成对腐败和极权的不满，学生呼吁经济改革和政治民主化。其后，全国其它城市也相继爆发大规模集会抗议活动。数以万计大学生冲击中共政要办公的人民大会堂、新华门等，要求与当局对话，学生们甚至在人民大会堂阶梯上下跪陈情，但中共不理会。4月 26日，官方《人民日报》发表社论，将学生运动定性为动乱。中共党内高层也出现两种声音，Zhao Ziyang等人认为学生是爱国的，但王震等保守派则不满，指Zhao Ziyang要葬送共产党。时任总理LiPeng和北京市长陈希同等则向邓小平谎报局势，夸大事实，令邓小平下定决心用武力镇压。5月 13日，学生发起绝食行动图迫当局对话； 5月 18日，LiPeng等出面与绝食学生对话，虚与委蛇； 5月 19日凌晨，Zhao Ziyang在时任中办主任Wen Jiabao陪同下，突然现身天安门广场，探望绝食学生，含泪勉慰学生爱护身体，显示他大势已去。当日深夜至 20日凌晨，中共召开党政军大会，总理LiPeng宣布北京戒严。<br />
<br />
港百万人游行　变「颠覆基地」 <br />
中共调动近 20万军队开往北京执行戒严，但遭学生和北京市民在各路口阻拦。 6月 3日夜，军队接令强行入城，大军从四面八方强行开进，一时枪声大作，学生市民纷纷中枪倒下，举世震惊的June Fourth大屠杀展开。事后，官方公布仅 241人死亡（包括戒严军人）， 7,000多人受伤；但民间和海外报道指至少有 2,000至 3,000人死亡。June Fourth事件是中共建政 60年来最残酷的一场镇压中国民主的行动，它导致国人心中的创伤不但迄今未愈，更因为中共拒绝认错、不停打压民运人士而继续溃烂。因事件下台的Zhao Ziyang被软禁 16年， 2005年 1月 17日逝世。香港民众积极支持八九民运，捐款捐物到北京，在June Fourth镇压枪声响后，百万市民站出来游行，抗议中共倒行逆施。June Fourth枪声击碎百万港人的心，中共则指斥香港俨成「颠覆基地」。中国组<br />
