<br />
「中华人民共和国中央人民政府今天成立了！」1949年10月1日下午3时正，北京天安门广场举行盛大的开国大典。首届中央人民政府主席毛泽东率领众新政府领导人，站在飘着五星红旗的天安门城楼上，面对站立着30万人的广场，以浓重的湖南口音向全世界宣告：中华人民共和国中央人民政府成立。1949年，历时三年的国共内战主要战役结束。共产党领导的人民解放军击溃中华民国国军，并逐渐控制了大陆许多主要省分。经过数月筹备，首届中国人民政治协商会议全体会议在北平召开，通过了政治协商会议组织法等纲领，决定以北平为都，并改名北京、以公元纪年、以《义勇军进行曲》为代国歌、以五星红旗为国旗，并通过了由180人组成的首届人民政协全国委员会名单，毛泽东为中央人民政府主席、其它六位为副主席。<br />
<br />
江山背后　是几千万条生命<br />
<br />
10月1日下午2时，政治协商会议首届全体会议选举产生的中央人民政府委员会。中央人民政府主席毛泽东，副主席朱德、刘少奇、宋庆龄、李济深、张澜、高岗，以及周恩来等56名中央人民政府委员会委员宣布就职。会议结束后，众领导人集体出发，乘车出中南海东门，前往天安门城楼出席开国大典。下午3时，毛泽东向全世界庄严宣告：「中华人民共和国中央人民政府今天成立了！」同年12月7日，中华民国政府正式迁往台湾。至此，中国历史上于海峡两岸分裂分治的政治格局，正式形成。自1921年7月1日，中国共产党在上海成立，1949年10月1日毛泽东登上象征皇权的天安门宣告新政权成立，28年里，中共付出几千万人的生命，终从国民党手中夺得江山，成为中国大陆新主宰。<br />
<br />
共产主义　令国人产生幻想<br />
<br />
28年里，共产党与国民党内战频频，加上日本侵华，日军在中国大肆屠杀，战乱频繁，令上亿中国人失去生命。中共新政权诞生，令饱受战乱之苦、备受外强欺凌的中国人，终于可以聊以喘息。由于中国共产党是「以实现共产主义为最高理想和最终目标」，令国人对未来新生活产生幻想。毛泽东宣告成立新政权之前，中共曾誓言旦旦，许诺建设一个民主富强的新中国，许诺将原来国民党一党独裁专政，变为各党派、各阶级合作的民主政体，许诺人民有言论、集会和结社的自由。开国大典上，毛泽东身边簇拥了大批非中共的民主党派和无党派人士，显示中共要高举民主的旗帜，团结社会各界力量，建设一个新中国。<br />
<br />
民主之旗被扔掉　走向独裁<br />
<br />
中共以民主为大旗踏出建政第一步，还表现在毛泽东于天安门城楼庄严宣读公告后的一个细节。据当时在城楼上采访的新华社前副社长李普透露，毛泽东读完公告后将文稿交给他拿去发表，同时交给他一个纸条，并叮嘱「照此发表，不要漏掉。」李普透露，上边是新成立的中央政府要员全体名单，因公告稿无这份名单，毛泽东要新华社补上全文照发。李普说：「我清楚记得，那份名单共56人，其中有27人是非中共人士，占了差不多一半。」名单显示中央政府很多重要职位都由非中共人士担任，成为中共建政后绝无仅有的历史纪录。他表示，令人遗憾的是，后来60年，中共把民主之旗扔掉，赤裸上阵，走向独裁，「民主成为点缀，有时甚至连点缀也不要了。」现在在中共内阁，非中共党员出任主要负责人，已如凤毛麟角，即便有部长是非中共人士，但也要听从部党组书记之命，俨如傀儡。</div>

							
							
							
							
