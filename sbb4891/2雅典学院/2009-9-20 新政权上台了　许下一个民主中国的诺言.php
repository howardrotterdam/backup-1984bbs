													<h2>香港故事从新华社到中联办　低调变干预　「中央对香港　要管都管」</h2>
						
						
																											<div id="postmessage_264408" class="t_msgfont">生果日报网上版 2009.9.30<br />
--------------------------------------------------------------------------------<br />
中共建政 60年：香港故事从新华社到中联办　低调变干预　「中央对香港　要管都管」 <br />
1949年新中国成立之初，总理周恩来即提出「长期打算，充份利用」八字方针处理香港问题，中共更早于 1947年便在港设立驻港机构新华社香港分社，证明对香港的高度重视。60年来，新华社香港分社（ 2000年起改称中联办）的联系职责没有改变，治港手段却由「揸大放小」走向「积极干预」。治港政策松紧与历任九位社长与主任性格差别无关，变的是中央治港心态，正如资深左派中人郑耀棠所指，回归后中央重夺特区话事权，「中央应该要管都要管。」记者：许伟贤　莫剑弦<br />
<br />
成立于 1947年 5月的新华社香港分社，首任社长是曾任周恩来秘书的乔冠华。虽然是中国官方驻港机构，但周恩来表明不急于收回香港，因此在乔冠华及其后两任社长黄作梅及王匡治下，新华社作风低调，罕有参与港务。<br />
<br />
许家屯较灵活　曾叫司徒华组党 放大图片 <br />
 <br />
作为驻港官方机构，新华社是请愿热点。<br />
<br />
八十年代中英谈判展开，肩负谈判重任的新华社积极联系各界，但未对香港事务指手划脚。适逢中国走向开放改革， 83年来港任社长的许家屯也紧跟中央指示，以较开明态度处事。前基本法草委、支联会主席司徒华接受访问时便指许家屯「比较灵活」，更曾一度叫他与李柱铭组党，作风显然与以往的社长不同。百万港人 89年上街声援学运人士，一下子扭转了北京向来较宽松的治港政策，当时任《文汇报》记者的时事评论员刘锐绍称：「中央好担心香港成为反共前哨基地；June Fourth后治港政策收紧好多。」最明显的例子莫过于中央 90年派出外交官出身的周南来港接任新华社社长，凸显香港不容「被搞乱」的重大意义，政制发展更不能撒手不管，由此开始了回归前长达七年的中英争拗，民主派与中方阵营僵持不下，刘锐绍直指「当时中英斗争意识，已经掩盖北京原本对港政策全面性。」 <br />
<br />
周南手段铁腕　姜恩柱「揸大放小」 放大图片 <br />
 <br />
新华社 2000年易名中联办，其后由跑马地迁往西环。 <br />
<br />
中英争拗在 97年 7月 1日落幕，中国收回香港治权，本可理所当然地在港推行高压政策，但出乎意料之外的是铁腕政策的周南功成身退后，换上予人感觉较为「和稀泥」的姜恩柱，紧守「揸大放小」原则，不常评论港务。刘锐绍分析，中央不想一收回主权便予人事事管的感觉，以免激起市民反弹，因此回归初年继续积极不干预，「插手情况相对上较少」；郑耀棠更指特区回归初年，中央基本上完全放手，连一些应该管理的东西都没有理会，丧失了主权应有的「话语权」。不过，这种相对少干预的情况，随着 03年 7月 1日 50万人上街而画上句号。 2000年易名为中联办的新华社， 03年后被指小至立法会事务委员会主席选举，大至区议会及立法会选举都事事干预。曾任基本法起草委员的民主党创党主席李柱铭就说过：「新华社不管的，中联办都要管」；中央欲在港建立第二个权力核心的野心，更在近期透过不少文章表露无遗。 <br />
<br />
建立权力核心　暂非中央政策 <br />
司徒华便以「加火」形容中央近年对港政策，「好似温水煮蛙睿个火煮得好猛，其实O本来目的都系睢！沟刘锐绍认为有关建立第二个权力核心的言论，暂时来说非中央政策，但无可否认凸显中国官场相当普遍的思维，「O从香港几年来施政磨擦睇到，香港欠缺一个能够领会中央精神班子。」特区未来的紧箍罩是否继续收紧？郑耀棠抛下这一句：「中央应该要管都要管；唔应该管，中央都唔会想管」，但他强调，中央加强管治并非干预。似乎也预示了特区的前路。<br />
