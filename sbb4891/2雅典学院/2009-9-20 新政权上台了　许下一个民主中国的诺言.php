&nbsp;&nbsp;<br />
June Fourth事件（又称June Fourth屠杀、八九民运等，中共称 1989年春夏之交的政治风波），是上世纪八十年代末发生在北京、最终影响全国和社会各阶层的民主运动。中共最后动用军队进行血腥镇压；中共总书记Zhao Ziyang被软禁至死；香港也因百万市民支持民运，被北京斥为「颠覆基地」。它的起因，与八十年代中国改革开放相关。中国从计划经济过渡到社会主义市场经济，由于市场混乱，通货膨胀，物价飞涨，官员家人子女以权谋私，批发倒卖稀缺商品谋利，引起社会强烈不满；时值国际共产主义运动陷入全面崩溃，反对独裁、追求民主成为共产国家普遍趋势，冲击一党专政的中国。<br />
