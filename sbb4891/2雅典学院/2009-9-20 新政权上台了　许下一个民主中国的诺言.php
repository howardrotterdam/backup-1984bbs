													<h2>程翔：新闻自由衰过以前</h2>
						
						
																											<div id="postmessage_261787" class="t_msgfont">生果日报网上版<br />
-------------------------------------------------------------------------------- <br />
八九学运期间，《文汇报》社论开天窗，只书「痛心疾首」四字；June Fourth后，中共清洗左报编采人员，无人再敢「造反」。当时为《文汇报》副总编辑的程翔说：「当年Jiang Zemin（时中共总书记）将全部左报老细召去北京，下令边个畀钱你，就要听边个话！」最令他感慨的是，June Fourth后香港传媒在内地采访越来越困难，新闻自由不断倒退。<br />
