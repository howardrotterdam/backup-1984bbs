无产阶级文化大革命（简称文革），是 1966年 5月至 1976年 10月发生在中国内地的一场动乱，实质是毛泽东为个人政治目的和理想，借群众打倒党内对手的一场政治运动。在长达 10年动乱中，中国社会陷于全面崩溃，w死人数达 200万，连国家主席亦不能幸免。中共自己也坦承，文革对中华民族，是一场「空前的浩劫」。对文革的起源，学术界有不同看法，一般认为，文革是毛泽东因大跃进失败后，在党内受排挤，大权旁落，忧虑中共路线与自己的期待背道而驰，又不能直接插手，于是用这种「自下而上」方式，拨航导向，并重新夺权。中共则将文革的起因，归咎于林彪和「四人帮」（即毛泽东夫人江青和其它三位靠造反上位的姚文元、王洪文及张春桥）借毛的旗号为非作歹。<br />
<br />
退居二线　江青出面发难 放大图片 <br />
 <br />
江青（右）在文革中充当毛的传声筒，煽动学生造反。<br />
<br />
因大跃进失败而退居二线的毛泽东，先从文化界入手，通过其夫人、时任中宣部电影处处长的江青出面，想从北京发难，矛头对准时任北京副市长、历史学家吴W，因吴创作了剧作《海瑞罢官》，毛认为，该剧是借古喻今、为 1959年反对大跃进，被他赶下台的国防部前部长彭德怀鸣w叫屈。但中共北京市委限制媒体批判吴W。毛泽东从北京发难不成，又借上海发力。 1965年 11月，后来成为四人帮重要成员的上海市委宣传部负责人姚文元，抛出《评新编历史剧〈海瑞罢官〉》，全国纷纷转载，「打倒资产阶级权威」一时兴起，文革导火线被点燃。 1966年 5月 16日，中共发出《五．一六通知》， 8月又发出《关于无产阶级文化革命的决定》，正式掀开文革序幕。文革初期，以文化界批「封建主义、资本主义和修正主义（简称封资修）」为主，但毛泽东不停煽风点火； 1966年 8月 5日毛亲自贴出《炮打司令部──我的一张大字报》；又发表文章和讲话，如「革命不是请客吃饭」，「革命是暴动，是一个阶级推翻另一个阶级的暴烈行动」，「运动的重点，是整党内那些走资本主义道路的当权派」等。 <br />
<br />
全国斗批改　刘少奇惨死 放大图片 <br />
 <br />
文革期间毛泽东多次在天安门检阅红卫兵，学生们无不激动万分。<br />
<br />
在毛的煽动下，文革迅速升温，造反派纷起造反，工厂瘫痪，学校停课，学生全国串连，到处搞「斗争、批判、改造」（简称斗批改）；时任国家主席刘少奇和中共书记处总书记邓小平，被斥为党内最大的两个走资派，被拉去批斗；刘少奇更妻离子散，惨死在河南开封，送去火化时连真名都不能用。全国帮派林立，都打着「革命」和「捍卫毛泽东思想」旗号，由「文斗」演化为「武斗」，真刀真枪上阵，打砸抢成风，死伤无数；各地的地富反坏右（即地主、富农、反革命、坏分子和右派），更被拉去游街批斗，活生生打死或被拉去「打靶」者不计其数。为阻武斗，毛派出军队到各地以「支左」（即支持左派）名义接管各级政权；又通过数次在天安门检阅红卫兵，将造神运动推向高潮； 1968年，毛开始倡导各地造反派夺权，重新成立「革命委员会」。经过一系列动作，毛泽东终于在党内恢复无可匹敌的威望，成为中共神坛独一无二的偶像。中国组 <br />
