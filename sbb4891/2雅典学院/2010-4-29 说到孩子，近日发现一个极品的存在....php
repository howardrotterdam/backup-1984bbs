<br />
&nbsp;&nbsp;李寻欢请求吕奉先杀死林仙儿，是为了阿飞吗？不是，是为了自己。是他自己觉得林仙儿是恶人，自己想杀死她，自己不希望兄弟和她在一起，自己想让兄弟离开她，忘记她，按照自己的想法活下去。还是那句话，越是觉得自己在为别人着想，往往就越是最大的自私，因为“我必须要帮助我的朋友”本身就是自己的想法与欲望。<br />
