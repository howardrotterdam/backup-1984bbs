&nbsp;&nbsp;在个人&gt;宇宙的大前提下，健康，父母，恋人，朋友，子女，金钱，汉堡包，干脆面，水杯，时间，模型，游戏机，漫画书，睡眠，话剧...这个世界上的任意两样事物，不管是有形的还是无形的，全都可以放在天秤上分出高低，计算出精确的价值。人类所做出的一切行为，全都像摆弄自己房间里的小物件一样，无论怎么摆放，怎么调整，也都一样是普通平常的举动，根本就没有什么对错与是非之分。<br />
<br />
&nbsp;&nbsp;所谓的“为别人如何如何”，全都是自欺欺人，自我催眠的假像而已。只要你没死，你的一切行为就都是一样的，不分大小，不分种类，绝无任何本质性的差别。<br />
<br />
&nbsp;&nbsp;捐助慈善事业也好，花天酒地去挥霍也好，都是很单纯的普通行为，我并不觉得二者有什么区别。同样都是花自己的钱，同样都是让自己满意，又有什么不同了？唯一的区别，也只是社会认同度有高有低罢了。胡乱挥霍，就会被一群莫名其妙的人谩骂鄙视。捐助慈善事业，就能得到很多人的赞扬和尊敬。可社会认知度又是什么东西？不也就是一群个体的集合吗？又有何价值？谁规定捐助慈善事业就是对？谁规定胡乱挥霍就是错？捐助慈善事业，一万个人支持，一百个人不屑一顾；胡乱挥霍，一百个人支持，一万个人鄙视，这不也只是数字上有差别而已吗，有何本质区别？这个世界上根本就没有绝对意义上的善。<br />
<br />
&nbsp;&nbsp;同样道理，这个世界上也没有绝对意义上的恶。什么叫恶？屠宰场杀猪杀鸡算不算恶？明明是很残忍的杀生，但几十亿人都要吃肉，这就变成了正当的行为，杀一万只都不算恶。但如果杀死一只大熊猫，仅仅是一只，就会成为被无数人唾骂的凶手。大熊猫和牛都是生物，也都是很可爱的大型哺乳动物，但为什么杀一只大熊猫就是恶，杀一万头牛都不是恶？表面上的原因谁都知道，那就是牛的数量多，大熊猫的数量稀少。但谁规定稀少的就该保护，数量多的就该杀？如果有人觉得牛很忠厚可爱，一只都不该杀，难道就错了吗？人类的数量更多，如果有人觉得应该消灭掉20亿人口，难道就错了吗？<br />
<br />
&nbsp;&nbsp;所以说，世界上并没有绝对的善与恶，无非就是支持者的多寡有所不同罢了。或者说，自己做的一切事情都是绝对的善与正确。只要你自己想清楚了，并且愿意去做的话，即使是强奸或吸毒也不能算是恶――既然愿意承担强奸之后被通缉/抓获/判刑的后果，愿意承担吸毒对身体带来的伤害和对生活的影响，那强奸与吸毒也便只一件普通的个人行为而已，并没有什么错误可言。也许50亿人都觉得这是错，是恶，但只要你自己不这么认为，那这对你来说就不能算是错。<br />
<br />
&nbsp;&nbsp;苏梦枕曾经说过这么一段话――“忠就忠，奸就奸，好就好，坏就坏，没啥大不了的。生就是生，死就是死，人活着，总要做决断，选错了，也不过是错了，选对了，也不过是对了，一刀子下去，砍的不是魔，那就是神，也没什么不可以的”。再进一步想的话，这个世界上其实根本就没有什么奸不奸，坏不坏，错不错，只有存在或不存在，做过或没有做。只要你做了一件事，这件事就会成为唯一且永远不可改变的绝对事实，并没有对错之分。<br />
<br />
&nbsp;&nbsp;世界上的任何行为都没有好坏，正邪，黑白，对错之分。不信的话，可以自己在脑子里想象一下，有什么事情是对的？又有什么事情是错的？就算想上1个小时，又能想出一件绝对的善事或恶事吗？肯定想不出。因为任何事情都不可能得到所有人的认同，你从老虎的口中救下了小羊，对小羊来说就是善，对老虎来说就是恶。只有不同的立场，而没有绝对的善恶。<br />
<br />
&nbsp;&nbsp;我到底算是个好人，还是个坏人呢。按照一般的解释，无非也就是“没有绝对的好人，也没有绝对的坏人”，“每个人的心中都有善念和恶念”。“双重性格，人格分裂，自己内心也很矛盾纠结”，等等等等。<br />
<br />
&nbsp;&nbsp;以前我也是这么认为的，但现在却觉得那类观点全都是屁话。既然根本就不存在所谓的善恶，自然也就没有什么好人和坏人。任何善恶标准都是人为制定的，没有任何善能得到所有人的认同，同样，任何所谓的恶也都会有人理解。把人强行划分成好人与坏人，本身就是一件很荒唐的时候。以我为例，帮助Y君也好，陷害Y君也好，都只是“我想做”的普通行为而已，完全没有任何不同。我的内心根本就不矛盾，从来都没有纠结过。<br />
<br />
&nbsp;&nbsp;爱护小动物也好，折磨小孩子也好，陷害兄弟也好，兄弟情义也好，全都没有做作，没有刻意，一切都是发自内心的感情。友情是真的，朋友是真的，想看朋友倒霉，想看朋友痛苦也是真的。我没有任何善恶观与是非观，只是单纯地想要满足自己的需求而已。活在这个世界上，其实就是在玩一场游戏，我希望触发什么样的经历，就努力去实现它。饿了吃饭，累了睡觉，想约朋友就约朋友，想害朋友就害朋友，仅此而已。既然我自己想要这么做，那我肯定就会觉得这么做是完全正确的。<br />
<br />
&nbsp;&nbsp;正确与否，唯一的评判标准就是我自己想不想。至于社会道德标准，我从骨子里就完全无视，因为那和我根本就没有任何关系。张三和李四都觉得屎很好吃，我会跟着去吃吗？王五和赵六都觉得穿红色外套是很一种下流的行为，我难道也非得赞同他们的主张？同样，就算全世界都觉得欺负小孩子是恶，为国捐躯是善，我也会保留自己的观点，绝不会盲从。所以，当我陷害过朋友之后，我根本就不会有什么罪恶感，更不会觉得自己不道德，或者对不起朋友。<br />
<br />
&nbsp;&nbsp;我要是在路边看见一只死麻雀，肯定会难受好久，半天都缓不过神。但得知前一段时间的那个福建南平小孩被杀事件时，我确实就是发自内心地高兴，做事情都精神百倍。像我这种心态，到底算善念还是恶念？肯定都不算，因为两个都是我的真实想法，并没有什么善不善，恶不恶的区别，仅仅只是一个平常人的心态而已。有人也许会说，我这种想法是畸形扭曲的，但我也可以说，像他那种同情小孩子的想法才是变态恶心的。他也是一个人，我也是一个人，他凭什么说我是不道德的？凭什么规定人类就必须同情人类？当我们的想法产生分歧时，我完全没有羞愧的必要。他并不比我高尚，我也不比他扭曲，只不过是他这种想法的赞同者比我的多而已。说到最后，仍然只是一个单纯到极点的“多数压倒少数”的问题。<br />
<br />
&nbsp;&nbsp;任何事情都没有绝对的正确与错误，所谓的道德标准，全部都是人为制定的。归根到底，也就是把大多数人的价值观强加给少数人，并强迫他们接受。少数人本来并没有任何过错，但在人为划定的道德标准面前就莫名其妙地变成了恶人，坏人，没有道德的人。正如那句话所说的一样：道德本身就是不道德的。<br />
<br />
&nbsp;&nbsp;任何道德准则都是人为制定的，今天的道德，以后可能就是不道德。我们现在每天都在做的普通事情，在几百年前，甚至是几十年前，就有可能是很不道德的行为。同样，很多我们现在根本就不敢去做的事情，在几十年后也许就是天经地义的正常行为。我们看西藏的天葬，觉得很野蛮，很血腥。但人家却觉得被秃鹰吃掉之后就可以升天，是至高的荣耀，把尸体直接埋在土里，任其腐烂掉，或者推进火炉里烧成灰才是残忍可怕，对死者大不敬的举动。各有各的文化习俗，谁也没资格说别人就是错的。所以，有时候不应该过分执着于所谓的社会道德，因为很多社会道德本身就很不道德。很多你一直都觉得天经地义的事情，静下心来好好想一想，又是否真的是天经地义呢？<br />
<br />
&nbsp;&nbsp;这个世界上唯一不变的永恒法则就是弱肉强食。除此之外，再没有任何其他的道德标准。而当人类进入文明社会之后，“弱肉强食”就进化成了“少数服从多数”，但本质上还是一样的，仍然是谁的拳头硬，谁就有道理。小到个人，大到国家，都是如此。<br />
<br />
&nbsp;&nbsp;有些事情，不同立场的两方人数比较均衡，便能形成和平的争论与探讨，无所谓善恶对错。比如说，马加爵和杨佳杀人，有一部分人支持马加爵和杨佳，觉得他们值得同情，另一部分人觉得马加爵和杨佳都是该死的凶手。由于持两种意见的人都不在少数，谁也无法彻底压倒另一方，所以双方就都不能算是恶。你就算支持杀人凶手，也不会被视为没有道德的恶人，因为有很多人支持你。而有些事情，不同立场的两方人数相差悬殊，无法形成势均力敌的争论，少数人就会成了不道德的恶人。比如说，杨丞琳说了一句“抗日战争才打了8年？南京大屠杀才死了30万人？”，立刻就遭到了上亿粪青的谩骂声讨，你要是再支持她，替她说话，就会立刻被归入到“不道德”，“人渣”，“垃圾”的群体中。按照社会上公认的道德标准来看，“杀人”和“说错话”的罪恶程度明显不可同日而语。但现在的状况却是，你支持杀人犯是道德的，支持说错一句话的人却是不道德的，这是不是很荒唐呢？这道德标准究竟体现在哪里呢？无非就体现在人数上罢了。人多就有理，人少就没理，就这么简单。<br />
<br />
&nbsp;&nbsp;举个最典型的例子――爱国。本身只是一种很普通的个人喜好取向问题，如今却俨然成为了衡量道德底限的标尺，如果有人表示自己爱日本胜过爱中国，肯定会遭受到猛烈的谩骂，并被归入到人渣的队伍中，这实在是让人无法理解。我住在10号小区，但我更爱12号小区，有人会说我不道德吗？没有。我身为一个北京人，第一爱上海，第二爱北京，有人会说我不道德吗？肯定没有。我身为一个亚洲人，第一爱欧洲，第二爱亚洲，有人会说我不道德吗？还是没有。那我身为一个中国人，第一爱日本，第二爱中国，又有什么错？哪里不道德了？说白了，还是那句话，多数人说你不道德，你就不道德。<br />
<br />
&nbsp;&nbsp;多数人代表不了少数人，就如同少数人代表不了多数人。如今已是文明社会，“少数服从多数”这一原则已经无孔不入地渗透到了世界上的每一个角落，凭个人的力量很难去改变。我们为了在这个社会上生存，便免不了要妥协。但妥协并不意味着要彻底抛弃自我意识，在条件允许的情况下，我觉得还是应该把“自己的想法”放在第一位。与其总是去想“这件事是否能被社会道德所接纳”，倒不如多想一想，自己觉得什么才是对的。当自己的想法与社会道德发生抵触的时候，根本毋须对此感到羞愧或耻辱，因为你并没有任何过错，只是和你持同一想法的人比较少而已。就如同别人都爱吃面条，你却爱吃米饭，别人都在玩PSP，你却只玩NDS一样，根本就没有什么可值得羞愧的。<br />
<br />
&nbsp;&nbsp;当然了，这并不是说应该肆意妄为。之前也说过了，想在这个社会上生存下去，便免不了要妥协。最起码，违法的事情是绝对不能做的。但不能违法的理由只是为了保护自己罢了，并不需要完全丢弃自己的道德观与是非观，完全盲从法律。因为世界上本来就没有公认的道德与对错，法律也一样只是多数人类个体的共识而已，并不能代表绝对的真理。有些法律，也许你确实很赞同，发自内心地想要遵守。但肯定也有一些法律是你不赞同的，只不过是为了自保才勉强去遵守。<br />
<br />
&nbsp;&nbsp;再举个例子。去年某日，我去一处不常住的房子取东西。由于时间比较紧，所以非常着急。上了电梯以后，我匆匆忙忙地按下了13层的按钮，刚想关电梯门，就有一个十岁左右的小孩冲了进来，一巴掌就拍在5层的按钮上。我当时连想都没想，直接一个大嘴巴就抡在那孩子脸上了，然后破口大骂道“操你妈，5层你还坐电梯！不会爬楼啊，懒逼”。到了5层以后，我揪住他，不让他出去，然后把电梯门关上，再到13层，我也没有出去，直接关了电梯门返回1层，然后把他给推出去了，自己重新再上13层。<br />
<br />
&nbsp;&nbsp;打人虽然不算什么大事，但肯定也是违法的。可当时电梯里只有我和那个小孩，没有别的人。而且我一般不住在那边的房子，3个月都不见得来一次，所以打了也是白打，绝不用承担任何后果。如果是在我常住的家里，就算遇到同样的情况，我肯定也不会动手打这个孩子，因为我怕被街坊邻居看到，然后找上门来骚扰，给自己增添麻烦。这就是我的法律意识，并不是为了道德而守法，而是为了自己而守法。<br />
<br />
&nbsp;&nbsp;一直以来，我的观点都是，唯一值得去遵守的东西就是法律，因为个人惹不起法律。只要不触犯法律，其他任何道德标准都没有必要遵守。这也就是我以前所说过的，除了法律之外，任何民间土规矩都是垃圾，想遵守就遵守，想不遵守就操他奶奶。原因很简单，不遵守法律会遭到无法抗拒的制裁（当然，就像上面说过的，如果愿意承担违法所带来的后果，那违法行为对自己而言也一样是正确的选择），而民间的土规矩并没有任何权威性，即使违反了也不会对自己造成丝毫伤害。所以，只有天生的奴才和傻逼，才会去遵守那些违背自己意愿的民间土规矩。<br />
 <br />
&nbsp;&nbsp;归根到底就是一句话，世界上没有善与恶，没有对与错，没有该与不该，只有想与不想。<br />
<br />
&nbsp;&nbsp;前一段时间，玩桌游的时候，听竹子说过他的一件往事，给我的触动很大――幼年时期，爷爷去世了。送葬的路上，自己坐在车里，一边看着爷爷的遗体，一边嚎啕大哭。但在伤心大哭的同时，自己的心里却同时有种强烈的兴奋感觉。因为这种新奇的经历是以前从来没有体验过的，所以觉得非常刺激有趣，心情十分激动。<br />
<br />
&nbsp;&nbsp;在有些人看来，说这种话也许就意味着冷血无情，大逆不道，没有人性。但我却明白，这才是真正的人性。爷爷去世了，肯定会难过。体验到前所未有的新奇经历，当然就会兴奋激动。两者都是最正常不过的反应，彼此之间并无任何矛盾，哪个也都不是假的。只是有些人迫于伦理道德的压力，不敢面对自己的真实感情，而是自欺欺人地选择逃避，在脑子里进行自我催眠，强迫自己忘记这种无法容于世俗道德，但却又真实无比的正常心情。<br />
<br />
&nbsp;&nbsp;我并不是没有道德，而是多数人的道德标准把我强行归入到了不道德的群体中。我并不是只接受和自己拥有同样价值观与道德观的人，只要是真性情的人，我都会很欣赏。如今仍然保持来往者，差不多都是自我意识非常强烈，并没有被那些莫名其妙的所谓社会道德所侵蚀的人。哪怕双方的观点有强烈的冲突，性格极不投缘，见面就要吵架，起码我也是在面对一个真实的人，而不是一个傀儡。在如今的社会，像竹子和范跑跑这种敢于正视自己的真实感受，并能坦然面对，而且可以不加掩饰地将其表达出来，即使不能被主流社会接受也丝毫不以为耻的人，实在是太少太少了。绝大多数人，已经被所谓的社会道德与伦理严重侵蚀，早就忘记了自己的真正本性，以及属于自己的道德与伦理。<br />
<br />
&nbsp;&nbsp;善也好，恶也好，这些人为划定的东西都不重要。人活一生，起码不能连最基本的自我意识都给丢失掉。</div>

							
							
							
							
