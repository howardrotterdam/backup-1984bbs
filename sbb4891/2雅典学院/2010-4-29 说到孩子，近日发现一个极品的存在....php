&nbsp;&nbsp;PS：说到这里，就不得不提一下范跑跑。我真是太理解他，太欣赏他了。当时刚听到关于他的事迹时，我真是激动得都要拍桌子了，因为这种真性情的人实在是太少见了，尤其是在中国。有很多弱智都恬不知耻地说，老师就应该舍命救孩子，这是最基本的义务，范跑跑就是个没有人性的败类。对于这种傻逼，我真是连骂都懒得骂。也有一些人比较明白事理，他们觉得范跑跑的行为可以理解，但应该低调一点，用不着大张旗鼓地宣扬那种不能被主流社会所接受的言论，如果他不这么高调的话，就不会被这么多人骂，也不会被学校辞退了。持这种观点的人，并没有真正意识到自己的生命有多么宝贵，所以才会把“被学校辞退”、“被人骂”这种微不足道的小事拿出来说事。而我和范跑跑是同一类人，所以完全能体会到他的心情。别忘了，范跑跑并不是个不通人情事故的单纯少年，他的职业可是个老师，他很了解这个社会，他完全明白高调宣扬“先管自己再顾学生”这种不和谐的言论会引起怎样的后果，但他还是要把这种观点大声说出来。为什么？因为他根本就是故意的，他根本就不在乎被人骂，被炒鱿鱼。在我们这种人的眼里，自己的命实在是太太太太重要了，相比之下，名声和职业真是连个屁都不算。他能死里逃生，保住性命，心中的狂喜已经到了无法抑制的程度，所以才会心甘情愿地抛弃一些微不足道的东西，比如说名声，比如说职业。当他接到解雇通知书，当他看到成千上万的人在大骂自己的时候，心里一定是开心无比甜如蜜的，因为越是这样，他就越能清晰地感受到“自己捡回了一条命”。如果换作是我的话，100%也会采取和范跑跑完全一样的行动――先抛下学生逃跑，再高调宣扬“老子的命最宝贵”的理论，然后成为全民公敌，尽情享受被无数人谩骂的快感。和宝贵的性命相比，职业和名声简直就像是拿在手里都嫌多余的垃圾，恨不得赶快扔进河里，还能打个水漂看。这就如同买彩票中了五百万，然后心情超爽，随手施舍给路边的乞丐两毛钱一样。<br />
