&nbsp;&nbsp;说完范跑跑，再回归正题。也许有人会觉得“个人&gt;宇宙”这种观点很荒唐，很意淫，只是在自欺欺人而已。理由很简单――就算你死了，地球也照样转，别人该干什么还干什么，不会受到任何影响。从某种意义上来说的话，这种反驳也是有一定道理的。但这种反驳却忽略掉了一个最重要的问题，那就是每个人的宇宙都是独立存在的，并不是同一个概念。有一万个人，就有一万个平行宇宙，有一亿个人，就有一亿个平行宇宙，各个平行宇宙之间并没有任何交集，“所有人的宇宙”是不存在的。任何一个人死了，他的宇宙都会整个消失，和别人的宇宙根本就没有关系。你只是你自己，而不是别人，更不是所有人，所以你只能以你自己的立场来思考问题，根本就无法进入到别人的宇宙，别人的宇宙和你根本就没有任何交集。如果你死了，你的宇宙就彻底消失了。既然整个宇宙都不存在了，哪里还有什么“别人”或“其他人”呢？这些东西已经全都不存在了。<br />
<br />
&nbsp;&nbsp;比如说，张三给他的儿子留了一笔遗产，他的儿子也许确实会收到，因为儿子的宇宙仍然存在，只是宇宙里的父亲去世了。但张三死了，他的宇宙就已经没有了，他既没有儿子，也没有遗产。再比如说，有一个人死了，我们仍然可以在少了一个人的世界里继续活下去，但这个人自己却无法证明自己死了以后，这个世界与其他的人仍然可以继续存在。<br />
<br />
&nbsp;&nbsp;正因为生命的特殊性，所以，“自己&gt;宇宙”的观点仍然是很有道理的。<br />
<br />
&nbsp;&nbsp;以这种思想作为标杆的话，也可以说，这个世界上所有的人都是绝对自我主义者，而且全都是100%纯粹的绝对自我主义者，只是自我的方式有所不同罢了。任何人的任何行为，归根到底也都是3个字――“为自己”。再说简单一点，任何行为都是单纯的无属性行为，没有任何额外的附加属性（比如善恶，对错，正邪等等），所有行为的唯一共同点就是“为了自己”。<br />
<br />
&nbsp;&nbsp;“为了别人XX”这种说法，并不是愚蠢幼稚或高尚无私，而是根本就不可能实现，不可能成立，只是一个假命题而已。只要人活着，他的一切行为就都是为了自己。有的人理解了这一点，而有的人却没有觉醒。<br />
<br />
&nbsp;&nbsp;从别人的角度来考虑问题，在我看来实在是一件荒唐到极点的事情。你不是别人，怎么可能站在别人的角度想问题？这本身就是一个荒谬的假设，根本就不可能做到。强行去做不可能的事情，就只会偏离预想的道路，让事情变得越来越糟糕。世间无数悲剧，都起源于“我这么做是为了你好”。<br />
<br />
&nbsp;&nbsp;古龙作品中的李寻欢就是个典型的例子，搞不清楚自己的立场，试图以救世主的身份“拯救别人”，结果也只是自作聪明，害人害己。既委屈了自己，又要遭人骂，被人恨，最后毁掉了恋人的一生，也毁掉了大哥的一生。即使如此，他也还是没有从中吸取教训，仍然愚蠢的“替别人着想”，居然能想出“雇人杀死林仙儿，以免让阿飞彻底陷进去”这种完全不考虑他人感受的计划，险些又毁掉了兄弟的一生。试想一下，如果李寻欢的计划真的成功了，阿飞就算看清了林仙儿的为人，肯定也会恨李寻欢一辈子。因为他的做法实在是太过分了，再怎么好心，别人也无法接受，这种所谓的“好心”，给别人带来的伤害要比恶意还大的多。<br />
<br />
&nbsp;&nbsp;如果我是阿飞的话，一定会立刻杀了李寻欢，这种人多留他活一天都是个大祸害。奸诈小人或野心家都可以防备，伪君子也可以看透，只有这种以圣人自居的变态一根筋才是最可怕的。他就算把你活活害死，也还会觉得自己是为了你好，只是你没有领会他的好意，辜负了他的苦心。完全就是个随时都会爆炸的定时炸弹。我宁愿和岳不群，戚长发，任劳任怨这样的人结拜为兄弟，也不愿意和李寻欢这样的人交朋友。和岳不群交朋友，只要没有特殊情况，他还真就未必会害我。如非必要，他肯定要把君子装到底，没准还会是个很好的朋友。就算真的设计把我害死，起码他也会承认自己是在害人。我既然算计不过他，被害死也无话可说，只能认栽。但要是和李寻欢交朋友的话，就算被害得生不如死，他也会觉得自己是一片好心，只是对方不理解他，这实在是让人死不瞑目。<br />
<br />
&nbsp;&nbsp;扯远了，回归正题――只要人活着，他的一切行为就都是为了自己。就像上面所举的例子一样，你越是特别想“替别人考虑”，“帮助别人”，实际上就越是自私，越是在满足自己的欲望。<br />
<br />
&nbsp;&nbsp;我买那个老太太的玩具，是为了老太太吗？不是，是为了自己。因为我觉得她可怜，所以想要帮她，目的是为了让自己的心情舒服一点。用30元换来心情舒畅，我认为值得。如果玩具要价300元，我肯定就不买了，因为我觉得不值得。<br />
<br />
&nbsp;&nbsp;父母养育孩子，培养他们长大成材，是为了孩子吗？不是，是为了自己。他们“自己”想要孩子，他们所做的一切都是为了养育“自己的孩子”，让自己拥有一个优秀的后代。如果孩子杀人放火强奸，最后进了监狱，父母也许就会和他断绝关系，因为他们不想要这种给自己带来负担与耻辱的孩子。<br />
<br />
&nbsp;&nbsp;朋友生病了，我牺牲假日去探望，是为了朋友吗？不是，是为了自己。我想巩固/维护自己与朋友之间的友情，所以才自愿去探病，仍然是为了自己。我不是弱智，也不是精神病，而是一个有思维能力的正常人。既然我没被任何人强迫，自己决定花费时间去做一件事，那就肯定有自己的目的。这目的虽然不是金钱，但也一样是目的，也一样是为了自己。<br />
<br />
&nbsp;&nbsp;某人收入微薄，却坚持捐助失学儿童上学，是为了失学儿童吗？不是，是为了自己。因为他看到贫困地区的儿童上不起学的时候，会难受，会心疼，所以他愿意付出自己的金钱，尽可能地把“自己的世界”里的某一部分改变成自己想要的状态，让自己不再难受。<br />
<br />
