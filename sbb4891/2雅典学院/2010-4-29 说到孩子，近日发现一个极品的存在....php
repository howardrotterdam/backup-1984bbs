&nbsp;&nbsp;但这一次，不知是怎么回事，我突然就觉得那个老太太很可怜，根本就看不下去。于是直接走到她面前，故意装傻问道：“是10元1个吧？”，没等她回答，我便数清数量之后说：“3个我都要了”，然后给了她30元，拎着玩具离开了。也许她是真的生活困难，必须要翻出点杂物，站在地铁口贩卖。也许她只是个经验丰富的商贩，专靠装可怜来卖东西，卖完一袋后马上再去拿一袋。但不管怎样都无所谓，反正我当时就觉得她很可怜，所以不由自主地就去买下她的东西。如果不这么做的话，我便会感到不舒服。<br />
<br />
&nbsp;&nbsp;事后回想，我也觉得自己当时的行为很反常，甚至可以说是荒唐。以正常人的眼光来看，我大概算是做了一件善事。但我能算是个好人吗？恐怕未必。好人不会在摩尔庄园里骗走小孩子辛苦玩了一年多的帐号，然后勒令他们用米币赎回，并且在骗光孩子们那少的可怜的零用钱之后仍然不还号，任凭他们哭叫哀求，绝望伤心，完全无动于衷。在喜欢小孩的人眼中，我绝对是个混蛋。<br />
<br />
&nbsp;&nbsp;但我真的是个恶人吗？我自己却也不这么认为。从小学到现在，每到下雪的时候，我都会买十斤小米，拎着沉重的口袋四处游走。每走一段路，便踢散一块积雪，然后把小米撒在地上，留给麻雀们吃。楼下的那些流浪猫，也是每天准时去喂，不管再累也会坚持，从无一日间断。在寒冷的冬天，我还会找来纸箱、布匹和海绵，然后缝好垫子铺在纸箱里面，手工给它们制作猫窝。这种事情，即使是普通的善人恐怕也无法做到。在爱猫者/爱鸟者的眼中，我可能又是个动物保护者的模范，超级大善人。<br />
<br />
&nbsp;&nbsp;我不仅看不得任何小动物遭受到痛苦，甚至对动物外型的假物都抱有强烈的爱心与怜悯。从小到大，我从没有粗暴对待过任何一件动物玩具，小学时买的毛绒娃娃全部都留存至今。因为我无论如何也忍受不了把小动物形象的东西扔进垃圾箱，哪怕知道它是假的也受不了，总觉得那样太残忍。每次在大街上看见被扔在垃圾堆里的小熊/小兔娃娃时，心里都会特别的难受，恨不得捡回家之后好好洗干净，然后让它们舒舒服服地躺在床上。说到这里，也许有人会骂我装逼装到了极点。但这也无所谓，因为我说的全都是实话，绝无一丝虚伪装逼的成份在内，自己心里明白就行了。<br />
<br />
&nbsp;&nbsp;这是善良吗？非也，后面会细说。<br />
<br />
&nbsp;&nbsp;同样，我在殴打小孩子的时候，可以心安理得地享受孩子的嚎哭，中途不但不会有一丝心软或负罪的想法，反而还会兴奋到勃起。<br />
<br />
&nbsp;&nbsp;这是恶毒吗？非也，后面会细说。<br />
<br />
&nbsp;&nbsp;我曾经有个情同手足的好兄弟，暂且就称呼他为Y君好了。使用“兄弟”这个词可能比较恶心，总有点小混混的感觉。我一向很反感那种刚认识没多久就称兄道弟的白痴，因此连“朋友”二字都很慎用，更别说是“兄弟”了。但此人与我的交情，即使说是兄弟也毫不为过。我们从五六岁的时候就在一起玩，一起打坦克大战，一起玩魂斗罗，一起玩FC热血足球，一起逃课去游戏厅，一起被初中生勒索劫钱，一起准备刀子和剪子，一起把劫钱的初中生扎得满脸鲜血，一起去各处旅游，一起准备中考和高考，进入大学之后，也经常去对方的学校探访，毕业后一起谈论工作与未来...算起来的话，交情已经超过20年了，当真算的上是同甘共苦的生死之交。有些人可能一辈子都没有一个这样的朋友。<br />
<br />
&nbsp;&nbsp;这位好兄弟很喜欢死攒钱，从小就是如此，平时极度节省，从不乱花一分钱。我非常反感他这一点，总觉得很恶心，根本就不像是一个小孩子。大约是在初一或初二的时候（95，96年左右），经过数年的FC/MD游戏多人混战之后，他已经深深地爱上了游戏，最后终于下定决心，要拿出积攒多年的存款（具体数目已经不记得了，大概是一千元左右，这个数字在当年便相当于现在的一万元，甚至更多），买一台属于自己的MD主机和几盘游戏卡。<br />
<br />
&nbsp;&nbsp;某个周末，我和他，还有我们的另一个好朋友F君一同出行，去买游戏机。出发之后，还没走出他家的小区，我们便被5名流氓拦下，索要钱财。面对着这些面容凶恶，年龄比我们大，体型又比我们魁梧的流氓，我和F君完全没有做出任何反抗，乖乖地交出了身上的所有钱财，并低三下四地恳求他们不要打人。而Y君当然不甘心把自己多年的积蓄乖乖交出，他疯狂地反抗，挣扎，但结果注定是徒劳的。我和F君完全不上前帮忙，只是站在一旁袖手旁观，他以一敌五，完全没有还手之力，被打的很惨。白白挨了一顿打，钱还是一分没有剩下，而且衣服还被撕坏了。<br />
<br />
&nbsp;&nbsp;此事对Y君的影响非常大，使他几乎崩溃。一个十多岁的大男人，坐在地上泣不成声，活像是一个讨薪不成，反被老板雇人暴打一顿的农民工。稍微冷静下来之后，他非常激动地斥责我们，问我们为什么不帮他。我的回答很简单，对方比我们强壮，人又比我们多，我们身上没带任何武器，正面冲突根本就没有任何胜算，只会激怒对方，招来更坏的后果而已。与其如此，还不如乖乖掏钱，破财免灾。这理由非常充分，合情又合理，Y君即使再怎么不甘，也无法进行反驳。<br />
<br />
&nbsp;&nbsp;事后，我主动借给Y君500元，让他再凑些钱，先把机器和卡买了。但他刚被洗劫一空，已经再无储蓄，除了每个月不到50元的零用钱之外，再没有任何其他的经济来源，根本就拿不出剩下的几百元钱。在之后的一段时间里，他几乎一分钱都不再花，连根冰棍都舍不得买，把所有的钱都死死攒下，希望能早一日把钱攒够。而我又主动借钱给他2次，每次都是100元左右，有零有整，号称是自己省下来的全部家当，让他先拿去用，不必着急还。对此，Y君很是感激。数个月之后，他终于拥有了一台属于自己的MD主机，还有几盘梦寐以求的游戏卡。所花费的金钱，有三分之二以上都是我借给他的。几百元在当时并不是一个很小的数字，像Y君这种零用钱很少，压岁钱又必须全部上交给家长的孩子，根本就不可能在短时间之内凑够。再加上我反复表示不必着急还，Y君便只是30元、50元的零散还了几次，然后便不了了之了。直到我们都上了高中，他还会时常提起此事，赞我是个很讲义气，很够朋友的实在人。<br />
<br />
&nbsp;&nbsp;但他却没有想到，那些流氓其实都是我找来的。我看Y君拼命死攒钱早就很不顺眼了，一直都盼望他丢钱，然后好好反省一下，认识到攒钱的错误，悔恨自己当初不该这么节俭。如此难得的好机会，我自然要好好利用。在我们约定好出行时间之后，我立刻就委托表兄，找来了几个有过劫钱经验的高中生，然后制定好作案计划，报酬就是劫来的全部钱财。可笑的是，那些高中生只是一群外强中干的窝囊废，表面上凶狠，其实心理素质也不怎么样。他们根本就没劫过这么多钱，当时是骑虎难下，无法停手，事后却越来越害怕，多次询问我会不会被警察抓，会不会进监狱，其中一个人甚至还想把钱还给受害者，在我的多次劝慰与开导之下，他们才能彻底安心放松。经过此事之后，Y君有了非常明显的变化，也不再死攒钱了，该花时就花，高中的时候还经常和我们一起去各处旅游。至于F君，也是知情人之一，虽然是在事后（事先告知的话，怕到时露出破绽）。十多年来，他一直保守着这个秘密，并没有告诉给Y君。原因很简单，他非常理解我的性格和思维，虽然并不能完全认同，但却仍然尊重及信任我这个好朋友。也正因如此，我们的友情才会一直维持到今天。<br />
<br />
&nbsp;&nbsp;既然设计害Y君，为什么还要主动借他钱呢？答案就是两个字：友情。看到朋友有困难，我就想帮助他，仅此而已，绝无虚假。找人害他是一回事，借钱帮他是一回事，二者互不相干，并无矛盾。我对Y君没有任何恶意与不满，确实把他当作一个非常重要的好朋友。但我看他死攒钱不顺眼，于是就找人把他死攒多年的钱全给抢走了。然后我爽了，满意了，出了一口气，目的也就达到了。接下来，我看见好朋友痛苦，自然就要帮忙，这也是理所当然的。虽然他的痛苦其实是由我造成的，但那并不是重点。反正就是这么一回事，理解我的人肯定能明白我当时的心理，不理解的人只会觉得我是个精神病，再怎么说也没有用，所以在这里也就不多解释了。<br />
<br />
&nbsp;&nbsp;另外，我当时的存款其实是比较充裕的，即使借他一千多元也没有任何问题。之所以只借5百，然后慢慢再追加零钱，一是为了显示诚意――“看我多讲义气，手头就只有这么一点钱，还要借给你”，二就是不想让他立刻买到游戏机。借他钱，是因为我的目的已经达到，既然自己已经爽了，便还是想帮兄弟一把。不一次借足，是因为我还没有彻底解气，希望他能多郁闷一段时间。如果什么都没耽误，仍然让他第一时间买到机器，我就无法彻底开心了。如此这般，我既卖了人情，又调戏了他。<br />
<br />
&nbsp;&nbsp;从始至终，我对Y君都没有任何恶意，在事发之前的一段时间内，我们之间连一点口角都没有发生过。我就是单纯地想看他郁闷，和友情/道德之类的东西完全无关，仅此而已。我害他的时候，并不觉得有任何不对，也不觉得对不起他，更没有任何负罪感。事后帮助他，安慰他的时候，也并不会觉得自己是在演戏，而是完全的真情流露。在之后的十多年里，我们仍然是很好的朋友，一起玩游戏，一起去旅游，该聚时就聚，该吵时就吵，丝毫都不觉得别扭。之所以这么坦然，是因为我打从心底里就没有觉得自己做错了什么，所以也就不会有任何心理压力。对我来说，这件事情和摆弄模型，整理书架等行为并没有什么本质差别，只是一个单纯的普通行为而已，目的就是为了让自己满意。再说直白一点，在我眼里，不管是亲人，朋友还是恋人，全部都只是活道具而已。他们的价值与死物没有本质上的区别，唯一的价值都是让我更加愉悦。<br />
<br />
&nbsp;&nbsp;像我这样的人，也许算是绝对自我主义者。这种人在社会上应该是有不少的，性格也是各不相同。绝对自我主义者的特质并不体现在表面，比如说任性妄为，性格恶劣，凡事都和别人斤斤计较等等。相反，这种人也许并不难相处，没准还很随和开朗，他们的自我主义主要是体现在深层次的意识形态中。同样的一件事情，绝对自我主义者与普通人的处理方式有可能会一模一样，但这仅仅是表像而已，其中的思考过程也许是截然相反的，只是最后走到了同一个终点。<br />
&nbsp;&nbsp;<br />
&nbsp;&nbsp;举个最简单的例子，和朋友约好去某个地方玩，到了约定的时间，朋友发短信说堵车，要迟到半个小时。普通人也许会表示谅解，觉得等待是理所当然的，因为他们的想法是“对方不是故意的，我当然得等”，“因为对方晚一点就翻脸，那不就成了混蛋吗”。而绝对自我主义者有可能继续等，也有可能直接扭头走人。但即使是选择了等待，原因肯定也是“自己很想去玩，不愿意取消，临时又找不到别人”，而不是“对方不是故意的，理解他”。等待也好，翻脸走人也好，都是为了自己的感受，而绝非是在体谅对方。如果有其他人可以叫的话，肯定就会直接和别人进去玩，然后把手机关掉，无视迟到者。再退一步说，就算有其他的人可以叫，也要继续等待这个人，那也是经过权衡之后，为了自己的利益而做出的决定。总而言之，不管怎么妥协，也都是在为自己考虑，脑子里想的永远都是“这样做的话，我会不会开心/难受”。绝对自我主义者的脑子里根本就没有“从别人的角度考虑问题”这个概念。<br />
<br />
&nbsp;&nbsp;再举一个例子，还是和朋友约好去某个地方玩，就在前一天的晚上，朋友突然打来电话说自己的爸爸得了重病，第二天去不了了，只得取消。在这种情况下，一般人也许会发自内心地表示理解，并安慰几句，绝不会有怪罪对方的想法。但绝对自我主义者的想法则是生气，而不是同情或替对方难过。他们也许会当场翻脸，直接开骂，也有可能会忍着怒火去安慰对方几句。但即使是违心劝慰，他们心中的真实想法也一样是“操，你爸怎么这么烦人啊”/“你妈逼，你爸生病和我有什么关系？让我扫兴，真混蛋”。<br />
<br />
&nbsp;&nbsp;如此解释，应该还是比较好理解的。由于人类无法脱离社会而独自存活，所以任何人都要抑制自己的情绪，谁也不可能做到绝对意义上的肆无忌惮，即使是奥巴马也不行。在通常情况下，绝对自我主义者的行为方式与普通人并没有太明显的区别。但在骨子里，他们的思维与想法却与普通人有着本质上的差异，他们永远都不会真正地站在他人的角度来考虑问题，他们的脑子里甚至连“为他人着想”这个基本概念都完全没有。一旦处在特殊情况下，这个差异就会瞬间体现出来。在情势必要的时候，就算把朋友活活害死，他们也不会有一丝犹豫或心理负担。就算有，也只是在担心害死朋友会对自己造成怎样的伤害，而不是对别人感到内疚。比如说，和朋友一起被困在雪山上，当粮食耗尽的时候，绝对自我主义者可以毫不犹豫地杀死朋友，靠他的血肉多活几天，绝对不会有一丝动摇。<br />
<br />
&nbsp;&nbsp;在十多年前我就说过，我最看不起的就是那些随意放弃自己生命的人，包括那些为国捐躯的所谓烈士，英雄。因为每个人的生命都是至高无上的存在，和生命相比，国家只是微不足道的灰尘而已，连个屁都不能算。这个世界上没有任何事情比生命更重要，无论发生任何状况（哪怕是祖国沦陷，主权丧失），也都应该把自己的生命放在第一位。人生就像是一个RPG游戏，但没有SAVE和LOAD的功能，游戏的机会仅有一次。只要自己死了，整个世界也就消失了，全宇宙都会化为虚无。整个世界都消失了，区区国家和主权又何足道哉？信仰和荣耀，更是如同灰尘一般微不足道。在那些“烈士”为了祖国抛头颅洒热血时，他们却没有意识到，自己已经抛弃了身为人类的基本尊严。<br />
<br />
&nbsp;&nbsp;后来发现，“自己&gt;宇宙”，“自己创造了宇宙”这种观点，早在几百年前就有人提出过了。简单解释一下，因为有你自己存在，所以你才能看到这个世界，感受到这个世界，这个世界上的一切元素都是围绕着你而存在的，你就是绝对意义上的核心。而一旦你死了，你就再也感受不到这个宇宙的任何元素了，这也就意味着整个宇宙都彻底消失了。所以，完全可以认为，宇宙是你创造出来的，你的命大于等于整个宇宙。<br />
