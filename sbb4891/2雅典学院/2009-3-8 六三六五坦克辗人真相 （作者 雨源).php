(这篇文章写于1999年，文中的20是俺篡改的，原文是10）<br />
<br />
“六三六五”事件已有近20年了，“六三六五”事件使一批人得了益，但更多的人却为它倒了霉。作亲历“六三六五”事件的普通学生，血腥惨案的见证人，看到和听到周围的人已经淡忘了此事，甚至有很多人认为“六三六五”镇压有利于国家的时候，心中特别悲哀。我想应该让大家知道一些实，请大家不要忘记因“六三六五”而牺牲的普通死难者和那些屠杀他们的刽子手们。<br />
<br />
六月三日下午，我在围观了西单公共汽车（“六三六五平暴”镜头中，几个学生端著枪向群众展示）和新华门附近小中巴（里面有很多枪支）后，来到了天安门广场。这时广场的帐篷里已经没有几个学生了，只是靠广场西侧的一个大帐篷下，还在展出著一些从进城便衣部队那里缴来的菜刀匕首棍棒和绳索等。尽管如此，大家当时仍然只是猜测，军队只是想化装进城，以便躲过市民和学生的阻挠。看起来，军队可能要把学生从广场撵走，清理天安门广场了。所以我们几个一起来的同学决定回校吃饭，晚上再来广场坚守。<br />
<br />
晚上七八点的时候，广播里传来北京市的通告和一些有关军队要进城的传言。我们几个刚吃了些方便面的同学商量了一下后，决定去广场支援那里的学生。然后，我们一行十几个人骑著单车，打著大旗，沿著学院路向南骑去。一路上，路旁的市民和学生不断地向我们欢呼，同我们一齐高呼口号，气氛非常热烈，但谁也没想到一场大劫正在向著北京袭来。<br />
<br />
当我们到达车公庄的时候，那里已有一串被群众自发拦住的军车，卡车上挤满了军人，他们都没有武器，只有少数几个车上的军人头上戴了钢盔。群众一见到我们举著大旗到来，马上跑过来拦住我们，请我们帮助指挥，拦住这些军车。于是，我们十几个人分成好几个小组，每组分别指挥群众包围著一个军车，向他们讲道理作宣传。同时，我们也负责劝阻老百姓，不让人伤害军人和军车。整个车公庄大街的军人和群众都在有序地僵持著，军人站在车上，群众和学生在车下宣传。<br />
<br />
约莫过了两三个小时后，大概在快十二点的时候，忽然有传说军队开抢了。不久，就见从二环路南面跑来了很多人，其中有的人身上沾满了血迹。这下，整个车公庄都乱了，群众再也控制不住了，他们开始群起掀军车。车上的军人只好跳下汽车，汇集聚拢到了一堆。这时，有的市民和学生开始打砸军人。当时我看到，有好几个可怜的军人的头被石头砸得鲜血直流。我们几个同学无助地看著发生的一切，看著这批可怜的军人簇拥一团，挤向了地铁站。<br />
<br />
群众饶过了这批军人，但无法饶恕这些剩下的军车。于是，一辆辆军用卡车和吉普被点著，我们亲眼目睹著这十几辆军车，化成了熊熊大火。约一两点钟的时候，我们十几个同学又聚到了大旗下，商量著下一步该怎么办。这时，我们周围围了很多市民，当他们听见我们要去广场的决定后，死活拦著我们，不让我们去白白送死。我们向他们解释说，广场上仍有很多同学在坚守著，我们一定要去支援他们，把他们救出来。在我们的坚持下，最后他们同意放我们去了，但坚决不让我们打著大旗去，因为听说军人一见大旗就开枪。这样，我们只好把大旗交给了这些素不相识的市民，开始骑车向复兴门奔去。（“六三六五”后，我们几经打听，得知市民已把大旗完好地保存起来。他们告诉我，待到“六三六五”平反的那一天，他们一定会把它再打出来的。我非常感激这些不知名的父老乡亲，正是这些正义的人们留住了这面红旗，使我们几个能够化险为夷。否则，我们也许就象那些在长安街倒下的学生一样，永远也回不来了，因为我一直都在打著那面大旗，而我的几个同学是始终都在大旗的四周的。）<br />
<br />
我们骑著单车从复兴门上了长安街。这时间正是军车已经开过去了的空隙，我们沿著长安街向东骑行，路上没遇到什么险情。当时的长安街灯光昏暗，充满了血腥和恐怖，街两旁的临时工地的围墙和薄铁皮门上布满了枪眼。当我们快到六部口的时候，一辆正燃烧著的装甲车里面的子弹还正在“霹雳啪啦”的爆著。这时候，西面开来了一些军车，我们十几个人马上和周围的人一起躲向了路边，我们十几个人也一下子失去了联系。我们其中的一个同学就是这时中了一枪。侥幸的是，他当时正半趴在另一个同学的头上，胳膊搭在中间，子弹正巧从他的头下和那个同学的头上，穿过了他的右臂，若子弹或上或下一点，那就肯定会击中他的或另外那个同学的脑袋了。<br />
<br />
当时，我和另外一个同学小王仍没跑散，待军车过后，我俩开始小心翼翼地步行，沿长安街南侧墙跟儿向东移去，我们的生死经历就从这开始了。<br />
<br />
这时的六部口与新华门之间的长安街上空无一人，不时的枪声加上昏暗的灯光映照下的长街，让我们觉得特别恐怖。我和同学小王沿著长安街的南墙根儿，慢慢地向东试著移动，深怕半中间杀出一路军人或扫来一梭子子弹。还算走运，我们没遇到任何意外便到了新华门的附近。当时的新华门附近就象死一样的寂静，甚至连个站岗的都看不见。当时我们想，那门里面一定埋伏了许多军人，一旦有人靠近，肯定必死无疑。我和小王最后还是颤颤趔趔贴著南墙根儿，挪过了新华门对面。再往前走些，我们终于遇到了一群市民和学生（约有几十人，看上去多数是学生）。我们一见到这么多人，胆子马上又壮了起来，刚才的恐惧也顿时消失了。<br />
<br />
不一会儿，从长安街西面，又开来了一大批军车和装甲车，卡车的四周围著帆布篷布。我们一下子都闪到了路两旁。由于长安街太敞，根本无处躲藏，我们只好趴在人行道上或蹲在小树后。不过，这批军车倒是开枪不多，只是偶尔地，从驰过的卡车的两侧冒出几枪。由于长安街两旁连个石头也捡不到，我们只能躲在树后漫骂，也有几个人撬起了人行道的方砖，摔碎成小块后，向驶过的军车投去。<br />
<br />
这次军车断断续续地过了约半个小时。之后，我们的人群又开始聚到了一起，这时的人数已比刚才多了些，大家开始相互攀谈起来。当谈到无人得知天安门广场的情况时，大家都想冲进广场，去救那里的学生。也不知谁先打的头，我们相互挽起手来，横在长街上组成了一个人墙，然后开始唱著歌（我记得好象是国歌和国际歌等），手挽著手，向东面的天安门广场行进。当行至离大会堂西侧路约一百多米时，我们已经能看到路前方站列的军人人排了。我们仍然继续高唱著前进，当我们行至离军人不到几十米的时候，我们已能模糊地看到前面的军人排正在平端著枪对著我们。突然，我们看到了正前方辟雳的火光，同时也听到了密集的枪声和哭叫声。前面开枪了，我们的人排一下子倒下了许多，人们一下子就散了开来。我右边挽著的一个学生，一个踉跄倒了下去，我差一点被他带倒在地。我猜他是中枪了，忙和另一个人把他架起来就往回跑。所幸，军队并没有追赶，最后我们在离军队大概二百米的地方停了下来，军队这时也停止了射击。（我清楚地记得当时的情形，当时，的确很多枪都是朝天放的，否则的话我也没命了，但我肯定当时也有不少是平射的，因为我们当时一下子就倒下了许多人。）<br />
<br />
中弹的人们很快被车推人背地架离了长安街。过了好大一会儿，人们才又聚了起来，这时我们的人又多了一些。我们又开始组成人墙，手挽著手，高唱著歌向天安门方向行进。和上次一样，当我们离军队几十米的地方，军人又开了枪，我们又被打了回来。这样反反复复约有四五次，每次我们的人数都在增加，而每次也都有中弹的被架回来。只不过，中弹人数远少于第一次的罢了。记得有一次，我旁边的又一个同学中了弹，被我和另一个人架著拖回来。我后来才看清，他的裤子大腿上被打了个黑洞，黑洞里一劲儿地向外冒血。虽然每一次前进，我和小王都是走在头排，但幸运的是，我们俩一直都没被击中。<br />
<br />
记得最后一次行进时，天已经亮了。当行至离军队约有五十米的样子，我们已经能看清对面的军人，他们正平端著枪对著我们，我们几乎隐隐约约地看到了对面黑洞洞的枪口。因此，我们自然地放慢了前进步伐。当时，由于长安街太宽，我们的人排自然形成了弧形，我和小王站在中间偏右的地方。当我们距军队约五十米的地方，路两侧的人们已经距军人只二三十米了。可能是天亮的原因吧，这次虽然离军人非常近，但中间的军人只向天上鸣了几枪，只是路两旁多了一些手举大白棒子的军人，不断地挥舞著大棒，追打路两旁那些靠近他们的人群。这时，我们也不敢再往前了，只稍退了一点，开始和军队人排对峙站著，中间约有五六十米的样子。<br />
<br />
后来，我们开始高呼口号，唱国歌和国际歌等歌曲。对面的军人听到我们唱，也和我们对著呼口号和高唱歌曲。只要稍有人向前，路两旁的大棒就追打过来。因此我们中间始终与前方的军人保持著一定的距离，我们就一直这样僵持著。大约过了半个小时的样子，奇怪的事情终于发生了。正前方的军队人排突然停止了呼口号和歌唱，人排中间突然撤开了一个巨大的口子。<br />
<br />
我和周围的人都愣住了，都不知道前方发生了什么事。正在我们发愣的时候，前面传来了“轰隆”声，这时，我们才看到，从广场方向开来了一队坦克，为首的一辆开到军队人排撤出的口子后，停了下来。接著后面又上来了很多坦克，它们开始列队，在第一对并排的坦克后，排著第二对，并且它们错列行在第一对坦克未能覆盖的空间上，然后是第三排。这样一来，整个长安街便布满了坦克，再无它们不能碾过的地带。我们看到这，才定了点心，猜测军队可能是想用坦克代替军人和我们对峙的。<br />
<br />
然而坦克并没停下，反而开始向我们开来。这时候，也不知道那个不要命的，首先躺到了马路上，别的人看了，也跟著躺了下来。转眼已有数百人躺了下来，宽敞的长安街上黑压压地躺了一片人。当时我和小王都站在前排，看到别人都躺下了，也就一闭眼随著躺在了路中间，心想是死是活随他去吧。我转念一想，要牺牲也得牺牲得壮烈点吧，所以才又睁开了眼。当时我和小王都在第一排，我是头朝西躺的，所以能看到东面坦克开过来的情形。<br />
<br />
坦克对著我们越开越快，马上就要碾到我们的人群了。我看得清清楚楚，当第一辆坦克马上就要压到我北侧五六米远的人群时，突然一个急煞车，急停了下来，我记得当时的马路被震得乱晃，整个坦克的上身都往前冲了一下，在离第一个人不到一公尺的地方才停住。我正前方的坦克这时离我还有十几米，也随著第一辆停了下来，接著所有的坦克都停了下来。紧接著，坦克仓门打开，军人开始向我们和路两旁扔毒气弹。霎时间，黄烟开始弥漫长安街，我和小王几乎随著所有的躺在地上的人们，一下子跳了起来，逃向了路南侧。我跳到路边，顺便往东看了一眼，当时我的肺都要气炸了，刚才与我们对峙的士兵们，看到我们狼狈鼠窜的样子，正在举枪跳跃欢呼著，我这一生都是不会忘记这批惨无人性的邪恶军人的。<br />
<br />
<br />
毒气弹象易拉罐大小，当时吸到肚里，只觉得舌干胸闷，直想吐，我和小王开始不住地干咳。这时，我和小王看到一个刚扔到身边的毒气弹，他捂住嘴想检起来扔向坦克，我看著黄烟“噗”地一下涌了出来，小王几乎被掀翻在地。我对他喊了一声“跑吧！”我俩几乎同时开始向西跑去。这时的坦克已经形成正式队形开始向西压。由于路南道窄，毒气也太多，路北是中南海院墙，因此，我俩想斜著穿过长安街，沿著中南海院墙往西跑，这样也许会安全些。所以，我俩斜穿长安街向西北跑去。因为路上到处都是黄烟，根本看不清任何东西，我俩一下子就跑散了。也因为黄烟，我几次都差点儿撞到行驶的坦克上，坦克也差点儿撞到我。<br />
<br />
也许我命大，或许是当过运动员跑得快的原因，我终于幸运地斜穿过了长安街，开始沿著中南海的红墙向西跑去。由于这一侧是树木花园，坦克并没压上来，所以跑起来安全多了，只是吸的毒气太多，胸腔无比地难受。当我跑过新华门时，门前密密地站了一排士兵，他们全都倒背著手直直地立著，面无表情地看著我们跑过去。当时，我真怕他们把我们往公路中间撵，那样的话，我们可就要糟殃了。勉强跑过新华门后，我的胸部实在难受，只好蹲下来抓了一把泥水（喷水龙头溢出的）往嘴上一按，接著再往西跑。不一会儿，我便跑到了六部口。这时，坦克已经在我之前到达了，于是，我赶紧右转，向北又跑了几十米，当我看到已经停了许多学生，也没有坦克追过来时，才停了下来。当时，很多年轻人都正蹲在或趴在地上呕吐干咳，几个女学生模样的干脆横趴在人行道上，把头伸出路基干吐，样子看上去难受极了。我的胸部也非常难受，只得蹲在地上吐了一会儿，结果什么也没吐出来。过了一会儿，我觉著好些了，看到六部口的坦克已停住了，我想找找小王，看他是否也已安全逃出，才又壮起胆子往长安街走去。<br />
<br />
这时候，长安街上弥漫著的黄烟已经淡了一些，但稍远一点还是什么都看不见，因此谁也说不清究竟死了多少人。虽然当时的坦克还在发动著，但轰鸣声已小了很多。我隐约能听到六部口对面的哭声。我壮著胆子从最西面的坦克前绕了过去，来到了六部口十字路口的西南角。当时到处都是哭声，待我走近一看，我一下子呆了，眼泪就象流水似的一下子涌了出来，坦克附近的情形太惨了，我实在控制不住，放声大哭起来。<br />
<br />
<br />
五个被碾死的学生横乱地躺在靠近人行道的柏油马路上。最西面的一个离人行道二米多远，头朝著西北仰面躺著，脑袋中间开了个大洞，象豆腐脑一样的白脑浆，参杂许多红血丝向前刺出一米多远。另外四个倒在他的东面更靠近人行道的地方，其中两个被压到了自行车上，和自行车黏到了一起。我和周围的人站在那里哭了一阵儿，当我看到身旁有几个学生模样的，便哭著同他们商量看是否能把这些尸体抬走，免得被军人抢去消尸灭迹。那几个学生和我一起走到邮电所西面的一个胡同，向一个住户要了一块木板，约一米多长，半米多宽，钉在一起的木板子。在周围人的帮助下，我们开始一具具地抬尸体。<br />
<br />
当时的坦克就在旁边响著，我们流著泪，也顾不得害怕了。我已经记不清先抬的那一位了，好像是先抬的那个脑浆被压出来的学生。当时由于木板太短，他的头顶在我的肚子上，脑袋已经空了，但我的衬衣上仍然沾了一些脑浆。抬另一个内穿红背心的学生时，他的一只右臂和上身只联了几丝肉，基本上已经掉了下来，红背心和肉绞在了一起，大腿上的五花肉也翻了出来，我拿著他的右臂放到他的肚子上，然后把他抬了出去。还有两个学生已经和自行车碾到了一起，我们费了好大劲儿才将一个和自行车分开。而另一个被压得自行车的脚蹬子刺进了胸腔，我们实在无法将尸体和车子分开，只好连同压扁的自行车一起抬了出去。<br />
<br />
记得我们抬最后一个尸体的时候，从长安街西面又开来一批坦克。我当时正拿著木板朝东蹲著准备抬尸体，根本没注意背后的事。突然间，许多市民和学生都冲著我喊叫起来。我回头一看，一辆最边上的坦克，马上就要压到我了，我条件反射地扔下木板，跳到了人行道上。转眼这辆坦克已经驶过，停在了前方，再看一下刚才的木板，约一寸厚的木板的一角已被压得象麻片一样，我被刚才的情形吓出了一身冷汗。<br />
<br />
最后，我们终于把五具尸体抬到了街后的一个胡同里。当时，有人建议我们检查一下死者身上是否有证件，以免一会儿军人把尸体夺走了，我们还不知道死者是谁。我们翻了一下，也没发现什么，只从一个绿背包里，翻出一个女学生的学生证（女生名字我已记不清，只记得是北京ＸＸ学院的）。尸体总放在胡同里也不行，我们正发愁时，正巧来了个好心的个体司机，他流著泪建议我们把尸体送到政法大学去，以备作为法律证据。我们听从了他的建议，把五具尸体（一个连著自行车）抬上了他的小卡车上。由于害怕大路上军人抢尸体，司机师傅带著我们沿著小胡同开了很久，才上了二环路。路上他还对我们讲述了他这一夜是如何抢运尸体，一直到<br />
现在的所见所闻。由于五具尸体是迭放在小卡车上的，车子只好开得很慢，这时的汽车前后已经跟随了许多人。<br />
<br />
汽车上了二环路后，人们骑著自行车前后围著小卡车，护送著这辆小卡车行向政法大学，一路上护送的自行车群越聚越多，当我们到达政法大学的时候，已有数千师生在校门口迎著我们，我们的前后也跟了上千人。当我跳下汽车时，一位教授模样的老人，噙著泪水抱住了我，我就象一个在外受屈的小孩，好不容易回到家里一样，趴在他身上嚎啕大哭起来。我记得，当时整个大街上什么声音也听不见，满街都是哭泣声。<br />
<br />
最后，一位陌生人，让我搭上他的自行车把我送到了学校。当时已经是中午十二点左右了，许多同学已在校门口等了一上午，他们多数断定我是肯定回不来了。我们一见面，一下子涌到了一堆。至于后来的尸体存放和展览的事，具体的情况我就不清楚了。只是后来听说，尸体在政法大学的一楼大厅里，停放了好几天，北京市数以万计的人目睹了这五个被碾学生的惨状。<br />
<br />
值得一提的是，我的那个同学小王也已安全地在我之前返回了学校。我们跑散后，他也差点儿被坦克撞倒。当他跑到六部口的时候，看到一个躺在地上还活著的学生，臂膀几乎已经被碾掉了下来，已经处于昏迷状态。但惊人的是，他在小王的慌乱搀扶下，竟站了起来，并能坐上一个自行车，扶著自己的那只胳膊逃了出去。六三六五很久以后，一个偶然的机会，我得到了这个学生的下落。这时他已经永远地失掉了那只胳膊，也被迫离开了他所在的那所大学。<br />
<br />
我是流著泪，写完这段回忆的。我由于害怕牵连受迫害，一直将此事沉默著。看到“六三六五”事件已经马上20年了，我想我应该把我亲身经历的“六三六五”血案的一个角落告诉大家，请大家不要忘记“六三六五”的死难者和刽手们.......................</div>

							
							
							
							
