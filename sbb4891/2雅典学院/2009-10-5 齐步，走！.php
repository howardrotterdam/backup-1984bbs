																											<div id="postmessage_270793" class="t_msgfont">　　国庆60周年的大阅兵的分列式表演，其规模和严整程度上肯定世界空前绝后。我的意思是，即使在天安门以后还有类似的阅兵，将来世界上也不可能有在规模上超越它的。除非中国出现一位特别喜爱阅兵的领导人――理论上，以长安街的宽度，我天朝完全可以排出规模比现在大四五倍的方阵来。据说，大连海军舰艇学院就曾排练过千人以上的方阵。<br />
　　世界上像中国一样重视分列式表演，且水准可能与中国一较高下的，方今仅只俄罗斯、朝鲜而已。由于我天朝分列式正步流派与俄罗斯朝鲜不同，谁的更好看，留到后面再说，但就规模来说，中国的分列式每行25人，世界第一，朝鲜人以24人宽度屈居亚军，季军俄罗斯是每行20人，因为红场的宽度尚不及长安街，它不能排出更大规模的阅兵方阵。如有必要，以长安街的宽度，我天朝完全可排出每行40人以上的队伍来。当然，有人会抬杠说，阿根廷布宜诺斯艾利斯的七?九大街比长安街更宽，但是，阿根廷这样的国家比较适合搞出世界规模最大的狂欢节游行队伍，阅兵这样的事情，还是交给社会主义国家比较合适。<br />
　　今天，特别看重阅兵的国家差不多都有点非民主的嫌疑，因为民主国家，老百姓也好、军人也好，似乎不懂得什么叫大局，什么叫集体荣誉，像法国、印度这些民主国家，虽然也年年搞阅兵，但显然只是出于一种历史惯性，咸与狂欢的味道越来越浓，不但动辄拉上友邦的军队一起散步，而且军犬、骡马之类的动物也一道上街。这样的国家不可能指望能有钟表一样精准的分列式表演。<br />
　　至于分列式整齐与否威武与否，与一支军队的军威国威确实没什么关系，否则，中国军队就是天下第一，而严重落后于时代的朝鲜军队则是世界第二了。但与中国进行过多次联合军演的俄国人看来，如果与西方国家相比，俄罗斯军队的水平只是世界二流的话，中国军队只能算三流水平。<br />
　　被公认为世界上武备最强大最训练有素的美国军队，基本上没有阅兵传统。虽然这些西方国家有些场合也有分列式表演，但最能展现分列式庄严威武风采的踢正步，在美国和英国，是被废止的，因为在这些国家，踢正步被认为有极权国家象征的嫌疑。正步又称鹅步（goose-step），是德国人的发明，源于普鲁士时代，希特勒时期把它发扬光大到了极致。而普鲁士人击败法国一跃成为头号陆军强国后，正步就成了后进国家军队必学的课程。不过，今天在德国，踢正步与行纳粹礼一道被法律所禁止。<br />
　　热爱阅兵时的分列式表演，确实在今天是非民主国家的特征之一，不过分列式这种操练形式，现代的源头，只应当出现在真正的民主社会。――手边没有资料，分列式应当最早出现在冷兵器末期的瑞士，距今至少有五百年的历史。因为分列式这种操练，很明显是为了训练步兵方阵使用的。手持长兵器、密集站立，能迅速转向、快步前进的瑞士步兵方阵，必须要用分列式这样的方式进行日常训练。在中世纪的欧洲战场，骑士一统天下局面被终结，我以为，瑞士方阵的作用至少不下于火药武器的出现。在当时，只有瑞士这种完全由身份平等的自由民组成的国家，才能发明出无坚不摧的步兵方阵，而在同时代欧洲的其他等级制国家，绝无可能出现有如许勇敢精神的公民，大小封建主和骑士也不可能接受平民成为战场主角。受瑞士步兵方阵的影响，欧洲才逐渐开始有了接近现代意义的步兵。<br />
　　如果再往前推，说罗马、希腊这样的国家当年也该有分列式，答案是毫无疑问的，因为这种公民国家，密集的步兵方阵是其最显著特色，要发挥方阵的巨大威力，必须进行日复一日的训练。分列式的训练，恐怕是比刺杀动作更重要的训练科目，因为方阵的威力就在于组合成一个整体之时。<br />
　　有意思的是，步兵方阵恐怖的巨大威力早在希腊时代，周边各个民族就有所了解，但他们多半只能雇佣希腊人，而无法自己训练出一支同样的军队出来，大约这也是因为“不知自由为何物的野蛮人”多是临时被动员而来，虽不乏逞勇斗狠之徒，但作为一个集体，则根本缺乏必须的牺牲精神和纪律意识。类似的，瑞士方阵在欧洲横行数百年之后，才先后有了德意志和西班牙的山寨版，虽然瑞士人战场上不打瑞士人，但雇主还是得硬着头皮不敢得罪他们，毕竟最好用的，似乎还是瑞士步兵方阵。瑞士的步兵方阵，几乎每个方阵都是乡邻，这种天然的感情纽带产生的战斗力，确实无法靠简单模仿产生。<br />
　　顺带说一句，CCTV拍的一部《复活的军团》，有意无意地将秦始皇的军队往欧洲的希腊/马其顿步兵方阵和罗马兵团上靠，仅考虑到两种截然不同的社会组织形式，而不必去追究其地出土文物上的大量意淫说辞，就很难让人相信，当时的中国存在这种与自由民组成的步兵方阵相似的兵团。中国历史上唯一在精神内核上与希腊/罗马及瑞士步兵方阵有些微接近的，可能就是戚继光组织的戚家军与曾国藩组织的团练，两者都是靠高薪组织同乡同村人当兵，本质上，戚家军和湘军都是典型雇佣军。在中国历史上要往前再追溯这种同类的士兵，或者还有东晋时期的由流民自组而成的北府兵，无论如何不可能是秦始皇挨家挨户抓来的兵。本质上，商鞅变法之后的秦国军队，即便有大批悍勇之徒，按希腊人对他们见过的一切东方军队的说法，还依然只是“一群由被恐惧所驱使的奴隶组成的乌合之众”。<br />
　　回到分列式。在欧洲，战场上的主力由领主、骑士率领着临时拼凑起来的乌合之众逐渐变成训练有素的常备军人，或发轫于瑞士步兵，再经由瑞典人、普鲁士人、法兰西人的不断改进，才有了今天的样子，当然，其社会组织也在此过程中发生巨变，欧洲的封建制渐为君主集权所替代。而常备兵的操练之术，一开始就是分列式，只有分列式的严格训练才能消灭个体的差别，建立起集体感，使万人如一人。某种程度上，或许可以说，没有分列式，便没有现代陆军，虽然分列式的实际战术意义早已不存，但培育纪律、集体、服从的功用却依然存在。<br />
　　中国人最早见识到西方的分列式，大约是林则徐时代，当时林则徐等人英夷无膝盖的印象，或许是初次见到欧洲军队分列式中走正步的缘故，稍早一点，马嘎尔尼等人晋京职贡，也见过我大清阅兵的军威，彼此都觉得对方可笑，我大清以为，夷狄无膝盖，可用竹竿将之捅倒，想必很难爬起，而英夷则认为，且不论我军装备之落伍，单我阅兵将士身上无用之物挂得玲琅满目，甚至有打折扇的，军队威仪丝毫不见，想必是一支乌合之众。<br />
　　以后的屈辱历史，便是一次次见识到洋枪洋炮和齐步走的威力。中国人学习走正步，比引进洋枪洋炮来得要晚了半个世纪，因为光引进武器不行，还得按照西法改造人，有史可查的阅兵有正式的分列式和走正步，最早应该是袁世凯小站练兵时期。那是向德国人学的。<br />
　　也许西方发达国家早就过了热爱分列式的时代，也不认为其有特别的象征意味，――经过纳粹之后例外，但对一切曾经落后挨打过的国家来说，分列式却是展现军威国威的最好工具，其功用与摩天大楼差不多。对中国这种“一盘散沙”的国家来说，尤其如此。<br />
　　可惜的是，我红朝建军，基础是少数黄埔生的骨架加少广大农民，打胜仗靠的是革命化而非正规化，开国大典的受阅士兵最初不知操练分列式为何物，临时现学。中苏蜜月时期，全盘学苏联，解放军在正规化、现代化上下功夫不少，但毛太祖武皇帝本人山寨出身，对形式主义、教条主义那一套甚为抵制，中苏蜜月终结，中国军队便从其最接近现代化素质最高的巅峰迅速山寨化。当时的中国军队是全世界站出来仪容最寒碜的军队，以往年年都搞的国庆阅兵，随着我军山寨化的时代大潮一并取消。<br />
　　顺带说一句，毛废除军衔制，让中国军队成为世界上穿得最皱皱巴巴的军队时，毛本人和身边的几位亲密战友的军装还是与其他人略有差别。至少毛、林在文革时期一身军装露面的场合，他们的军帽并不是软乎乎贴在脑袋上，而是不经意地在前端隆起，形成类似大檐帽的效果，如非刻意定做，那就是手下精心地仔细抚弄过一番。<br />
　　可怜我们从幼儿园时代起就开始学“1、2、1”都想长大后当兵流血牺牲的小屁孩，连电影里看到朝鲜人民军都羡慕得要死，按照我们当时的话说，人家看上去就特“正规”。影像世界里出现过的军队，只比中国军队略微正规一点的，是日本鬼子，最正规的，无疑是德国兵，他们不但正规得一塌糊涂，而且还特别“派”， 虽然也是鬼子，但没祸害过中国，所以是私下里崇拜喜欢的对象，――到今天，热爱“第三帝国”的中国人，恐怕要比德国总人口还多吧，至于美国，从不能给人很“正规”的印象，看上去松松垮垮，说美帝是“纸老虎”，我信。但苏军是继德军之后世界最“正规”，电影《卓娅》里我们见识过苏联飞机遮天蔽日、坦克布满原野的可怕场景，“解放军不可战胜”的话说万遍也比不过那一次直观印象。中国惩罚越南时，我们那距苏联边境仅几百公里，银行取钱的人排成长龙，人们疯狂储备干粮。<br />
　　当我们第一次见到分列式时，世界各国军队“正规”排序榜立即倾覆。当时中国刚开始与外界恢复正常关系，电影院放映一冗长无趣的纪录片，从头到尾是各国民族特色队伍游行，当瑞士人出现时，所有打瞌睡的小男孩都如吸食了鸦片般振奋，片中瑞士人身着蓝色制服随着铁皮鼓表演分列式。当时我们真以为像大元帅一般华丽的制服是瑞士军队现在的装扮，啧啧称奇之余，暗叹我天朝军队何时能也正规一回，也能像瑞士人一样走操。我们毫不犹豫地把“世界最正规”的头衔给了瑞士这个“无害”的小国。<br />
　　中越战争后，我军终于换装，于我等热爱战争的少年，其欣喜若何？然后有了1984年的大阅兵，真是盼我军威国威盼了十来年啊。记得当年《兵器知识》“供借鉴但不代表我们认可其说法”地转载《简氏防务周刊》的评论文章时，对英国人对我军常规武器过时的说三道四非常愤怒，心说，等着罢，就你英国这种过气的二流国家，收拾了美国之后自会顺带收拾你们的。<br />
　　感谢CCTV，我是打那时起，才知道以前有过阅兵，才知道我军原来军威过，而我国原来国威过。而且曾威了超过十年。<br />
　　再然后，有了国旗班。这差不多是全世界走操最庄严精确的团体了，因为红场列宁墓和当年第三帝国的警卫旗队钟表一样精准的走操已成历史。得感谢国旗班，太阳-天安门-毛这三位一体，在毛空缺了二十几年之后，天安门莫名其妙地还作为国家的图腾出现，搞得我国还像个神权国家，而没有多少党国的意味。有了国旗班的分列式，在CCTV之类宣传窗口，国家的图腾才逐渐终由天安门悄然向国旗过渡。――想想看，如果没有国旗班，来自全国各地的游客到了祖国的心脏――天安门广场，他们该以何方式表达自己对伟大祖国的热爱呢。<br />
　　某些时候，作为一个中国人，这个国家确实值得自己为之骄傲、为之自豪的东西不多，而让你感到沮丧的愤懑和压抑的东西却无处不在。这个国家拿得出手的，让你骄傲自豪的东西，而且是其特有的，恐怕就是盛大的阅兵了。是的，只有中国才有可供阅兵之用的最宽阔的街道，只有中国政府才愿意为了人民的骄傲和自豪，全心全意地、不惜一切代价地打磨出世界上最好的分列式――让人民在电视上看见它。<br />
　　以我个人的审美观，我认为中国的分列式是有史以来最庄严最美的。它自成流派的步伐、步速、姿势最符合中国人的文化和传统，庄严大方不失洒脱利落。苏联人的步速动作明显大过中国，双手垂直，踢正步时，身体会前后微微晃动，有跳跃感，固然行走起来有种咄咄逼人的压迫感，但这种姿势行进，要保持整齐殊为不易，而其源自欧洲骑士傲慢传统的抬着脖子的姿势，在中国人学来，会显得格外滑稽。朝鲜人学的是典型苏联流，个小、短腿的缺陷使得朝鲜军队行进时，丝毫没有那种舒展的感觉，看上去一跳一跳，走得慌慌张张。纳粹德国的正步手臂摆动幅度接近中国，步伐动作稍大于中国，看上去是除中国之外最舒服的。<br />
　　在我看，中国阅兵最大的问题甚至是致命问题在于，它在长安街上行进。长安街及其周围建筑的尺度，几乎都不是按照人的尺度来的。天安门城楼是适合看群众游行的地方，它的高度、尺寸，让人像是阿兹特克人的祭坛，它适合半人半神的皇帝高高在上，下面是蚂蚁一般走过的迷狂的人海，然后是孩子们跑到祭坛下仰着头欢呼献花。<br />
　　――当然，上面的最好也是能在此时满面笑容地挥舞着帽子，向下面的人民致敬的伟大领袖，而不应当是像日本人开发的机器人一样，表情AI只有几行程序的首长。<br />
　　只要认真想一想，站在天安门城楼上看分列式，就是件极其滑稽可笑的事情。检阅者在那么远的距离，根本看不清走过的士兵的脸孔，看不清我军的威仪。――虽然首长们看到女兵方阵走过时，一张张紧绷的脸纷纷绽放出笑意，但请放心，他们远不如坐在电视机前的我们，他们只能通过服装和身姿看出是女兵，仅此而已。有一天技术发达了，弄一群机器人在长安街上走过，站在上面的人根本看不出来。当然，被检阅者也根本看不清上面的人是谁。<br />
　　事实上，由于放在这个不适当的街道行进，现场没有一个人真正处于最能感受到军队行进中那种气势和力量的位置。电视机前的观众看到的镜头，也统统是大角度俯视的。如果说这种角度能展现一支军队一往无前的气势和战无不胜的力量，我深表怀疑。中国阅兵式最缺的要素恰恰就是压迫感、力量感。只要是在长安街上阅兵给天安门上的人看，就是每行站100个兵，它也显示不出那种凛凛的力量来。<br />
　　回过头来看《意志的胜利》中希特勒在纽伦堡的阅兵。以现代的标准看，元首检阅的分列式非常山寨，且不说远不如我军整齐，就规模而言，限于街道宽度，每行只有12人，就是这样的宽度，片中可以看到他们在穿过街道的门洞或在街道转弯时，队列还要收缩一下。但元首是站在路边的奔驰车上，这个高度只比行进队伍高半人，处于一个与被检阅者互动的最佳距离。至于一般的围观群众，由于处于极近的距离――片中最远的围观者也比天安门城楼那个位置更近，不消说，他们在现场感受到的那种凛凛气势，要比我们通过电影看到的强过百倍。<br />
　　当然，像元首这样浑身带有魔力的人，天朝体制并不出产，我天朝较容易大规模制造勃列日涅夫同志这样的领导人，那么退而求其次，红场列宁墓那样位置也是个很好的检阅位置。甚至不追求或没有能力与被检阅者互动的领导人，就该在那个距离、那个高度检阅。<br />
　　公允地说，苏联/俄罗斯的阅兵式，虽然其整齐程度明显要逊于我国，方阵规模更是明显缩水一号，但观感上其气势则远非我军能及，实在不是我军威国威本身不如，乃是角度和距离的问题。任何队伍拿到长安街上遛遛，都渺小得没有丝毫气势了。至于元首在纽伦堡检阅的那支威风凛凛的袖珍军队，放到长安街上，恐怕会显得马路空旷得太别扭。<br />
　　话说回来，我天朝举世无双的分列式，仔细推究起来，原本就不是要展现给谁看的。当代法国、印度之类就不必说了，甚至纽伦堡，由于元首和纳粹是如此得人心，甚至老百姓都可以在马路边上自家窗户上随便看，而我们的阅兵，就像火炬传递一样，虽然声势浩大，不惜倾尽举国之力，但除了天安门城楼那些看不清分列式的人，其他中国人甚至看到的只是延迟了30秒的“现场直播”，本质上，说它更是一次体现这个国家某种必要的规格、资格、待遇的仪式，也许更合适。如果这场盛大的阅兵式，完全由电脑特技制造完成，对这个国家的老百姓来说，其实是一点遗憾都没有的。<br />
　　而对我这样一个真正的分列式爱好者来说，如果10年后，没有抬着第四块画像的游行队伍出来前的阅兵式可看，多少是件遗憾的事情。<br />
　　祖国，万岁！<br />
　　中国、俄国、朝鲜，齐步，走！<br />
