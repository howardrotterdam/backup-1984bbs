																											<div id="postmessage_539858" class="t_msgfont">社科院专家:学界基本肯定清明时节雨纷纷是伪诗<br />
一年一度的清明节即将到来，历代很多文人将清明节作为诗词歌赋的对象。如我们熟知的杜牧的诗句“清明时节雨纷纷，路上行人欲断魂”。“这首诗学界基本上肯定这是一首伪诗，第一，他很可能不是杜牧写的，第二，很可能不是为清明写的。”中国社会科学院文学研究所副研究员施爱东博士今日做客人民网《金台会馆》时指出。 　　施爱东说，清明是一个春天的节日，过去的时候有寒食、有清明、有上巳节，后来只剩下清明了，清明集合了过去所有春天节日的特征。有的时候歌咏清明就是歌咏春天，所以清明节的诗词既有上坟的悲喜交集的，也有是纯粹的高兴，就是游春喜悦的大量诗词。<br />
 <br />
<br />
　　“像我们现在所流行的‘清明时节雨纷纷，路上行人欲断魂’，这首诗学界基本上肯定这是一首伪诗。”施爱东说，第一，他很可能不是杜牧写的，第二，很可能不是为清明写的，因为这首诗词最早出现在南宋末年，过去编杜牧各种各样的集子，这首诗从来没出现过，研究杜牧的人也根本不知道有这首诗，后来突然冒出来，标了是杜牧(写的)，最早的时候不叫《清明》，叫《杏花村》，实际上就是一个人在清明时节找酒喝，并不是一个人去上坟，心里很悲悲泣泣，且不说这首诗的作者是谁，主要是他不是去上坟。上坟主要是带着酒、肉，祭过祖先的人叫做“作肉”，就是有福气的，祖先喝过的酒，我们叫“作酒”，这些酒肉是不能浪费的，一定要吃掉，所以唐代形成的习俗就是先去祭祖，然后把这些酒肉吃了。如果写这首诗的人应该自己拎着酒，不然是违背常理的。所以这首诗词可以肯定的是即便是杜牧的，也不是去上坟.....</div>

							
							
							
							
