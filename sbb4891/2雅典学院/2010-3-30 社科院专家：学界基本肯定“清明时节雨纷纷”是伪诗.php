<br />
谈歌：“清明时节雨纷纷”是伪诗吗？(2009-04-04 09:15:01)转载<br />
标签：《清明》伪作选本杜牧杏花村诗歌文化杂谈&nbsp; &nbsp; &nbsp; &nbsp; 分类：杂谈<br />
 <br />
今日是“清明”。<br />
<br />
上午到街中购物，有儿童吟诵杜牧诗“清明时节雨纷纷”。谈歌听的怔忡，抬头一看，太阳鲜亮，何必强说“雨纷纷”呢？不禁哑然失笑。<br />
可以说，此诗为国人最为耳熟能详的诗篇之一，或是国人皆能出口成诵。<br />
<br />
清明时节雨纷纷，<br />
路上行人欲断魂。<br />
借问酒家何处有，<br />
牧童遥指杏花村。<br />
<br />
如周汝昌先生所说：“这首小诗，一个难字也没有，一个典故也不用，整篇是十分通俗的语言，写得自如之极，毫无经营造作之痕。音节十分和谐圆满，景象非常清新、生动，而又境界优美、兴味隐跃。诗由篇法讲也很自然，是顺序的写法。第一句交代情景、环境、气氛，是“起”；第二句是“承”；写出了人物，显示了人物的凄迷纷乱的心境；第三句是一“转”，然而也就提出了如何摆脱这种心境的办法；而这就直接逼出了第四句，成为整篇的精彩所在―“合”。在艺术上，这是由低而高、逐步上升、高潮顶点放在最后的手法。所谓高潮顶点，却又不是一览无余，索然兴尽，而是余韵邈然，耐人寻味。这些，都是诗人的高明之处，也就是值得我们学习继承的地方吧！”周老先生所言极是，这首诗用词明丽，写景清新，语言通俗，境界优美，应该是写清明的杰作。更有人说，杜牧的这首《清明》诗，使千百年来所有写清明的文学作品相形见绌。这个判断，大抵也是不会错的。<br />
<br />
但是，这首诗却一直受到质疑。有人干脆讲，此诗是伪诗，不是杜牧先生写的。<br />
<br />
理由呢？却也简单：杜牧这首《清明》诗，不见于杜集。<br />
<br />
也就是，杜牧的文集中没有这首诗，于是，学界很多人便认定是伪作。对么？<br />
<br />
再如三联周刊的朱伟先生说：“中华书局出版的《杜牧资料汇编》，翻阅极困难，因是一个个历代人物罗列，只能一个个查找关于这首诗的相关资料，其中收集了唐到清300名家对杜诗的评点，只有一人说到了此诗。可见宋之后的学人，起码都不认为此诗代表了杜牧的水平。”朱先生由此质疑：《清明》诗可能是一首伪诗，因为不见于杜牧诗集。而且，朱先生考证，唐至宋，清明节气并不受重视。朱先生还翻检了经常阅读的由中国社会科学院文学研究所编辑，人民文学出版社1978年版的《唐诗选》，在杜牧名下，也不见这首《清明》诗。以《清明》诗的知名度、艺术成就，被选本弃用，是很奇怪的。<br />
<br />
网友范先生则说，他为了进一步了解《清明》真伪的有关情况，曾上网查了一下。《清明》诗最早见于《贵池县志》，最早由南宋时人收入一些诗歌选本。关于《清明》诗是否由杜牧所作的争论早就有。根据有关文章，《清明》的产生、流传、作者，都是问题，不能肯定为杜牧所作，但也无法否定。<br />
<br />
还曾有诗友说：几年前，曾读南宋人编的《古今岁时杂咏》，在“清明”卷下，没有见到杜牧这首著名的《清明》诗。当时感觉奇怪，只是没有深究，以为是编者眼光狭隘，疏漏了。现在看来，这其实是问题所在。云云。<br />
<br />
《唐诗鉴赏辞典》第二版特增补了一条注释，指出，“宋初乐史《太平寰宇记》，第九十卷《N州（今江苏南京）》中已有‘杏花村在（江宁）县西，相传为杜牧之沽酒处’之记载，可见至少在五代是就已将‘借问酒家何处有，牧童遥指杏花村’的作者定为杜牧，故伪诗之说尚无确证。”<br />
<br />
或许，《唐诗鉴赏辞典》说得更客观些？<br />
<br />
唉！杜先生已是古人，不能顶着烧纸从坟里跑出来作证了。或伪或真？读者自家评判即是。无论怎样，《清明》仍是一首传唱不衰的好诗么！<br />
<br />
如果没有进一步的证据，上述几位质疑者的说法，大概只能是猜测了。不作数的。<br />
