													<h2>共产党宣言――通往奴役之路的金钥匙</h2>
						
						
																											<div id="postmessage_582075" class="t_msgfont">共产党宣言――通往奴役之路的金钥匙<br />
<br />
作者:再世关羽<br />
<br />
总是使一个国家变成人间地狱的东西，恰恰是人们试图将其变成天堂。――荷尔德林<br />
<br />
应学校要求，硬着头皮读完了《共产党宣言》。果不其然，马克思以他过人的聪明才智和刻骨的仇恨，编撰了这样一部杀气腾腾的马版《我的奋斗》尽管整部书中充满了对虚无缥缈“共产主义社会”的美好描述。但可惜，《共产党宣言》中的最后一句话“让统治阶级在共产主义革命面前发抖吧。无产者在这个革命中失去的只是锁链。他们获得的将是整个世界！”彻底的揭开了共产主义的真正面目――纳粹主义的鼻祖，二十世纪共产主义革命中血腥与仇杀的根源，一部“每一个字都是用鲜血写成”的撒旦圣经。<br />
<br />
在昏暗的灯光下，犹太胡子马克思，瞪着他那扭曲的双眼，一字一鞭的描绘出了他眼中充满血腥和剥削，让他这么个“乱世奇才”无法一展抱负，只能生活在贫困当中的“资本主义社会”：“资产阶级社会早就应该因懒惰而灭亡了，因为在这个社会里劳者不获，获者不劳。……资产者唯恐失去的那种教育，对绝大多数人来说是把人训练成机器。”<br />
<br />
然而，马克思自诩“经济奇才”，“在大英图书馆里耗费了二十年时光”，却“无意”的忽略了这样一个基本的经济学常识――只有在交易双方都认为有利可图时，交易才会发生。总所周知，雇佣劳动，也是交易的一种，如果交易双方有一方被另一方剥削，那这场交易怎么会发生呢？<br />
<br />
马克思认为，工人们受到了剥削。但是，为什么他们宁愿背井离乡的来到城市，甘愿受到资本家的剥削，而不是继续过那种农村，没有剥削的田园生活，不用深想就会明白，他们之所以不愿继续在农村待下去，是因为农村的生活（除了勉强填报肚皮而外没有什么货币收入且只能置于无所事事的境地，远离现代文明可能提供的机会）比起目下的生活来更不值得选择。在马克思对所谓的&quot;血汗工厂&quot;的谴责中包含着多少对工人的真诚考虑，而多少是为了满足自己在的欲望和野心呢？还是整个就是对工人的智力和他们所选择的生活的蔑视，根本无视他们的生活而强要为人作主的伪善呢？这种吝啬的同情心（自己不创造也不付出），只是想通过违背正义的方式去行使&quot;分配正义&quot;，必使最坏的狐狸跑出来主持分配，而使价值的创造者遭人鄙夷：节俭的资本家被视为守财奴，勤谨的经理人被视为吸血鬼，老实本分的工人成了工贼。他们使整个社会看重的是绿豆汤而不是长子权，这是社会主义国家中的工人最后堕入打着社会正义名义宰制社会的不治创造破坏生产的社会主义（实为国家社会主义）官僚们的工奴命运的根本原因。<br />
<br />
马克思口口声声要”建立人人平等&quot;的共产主义天堂。然而，为了建设这个“人人平等”的所谓天堂，马克思却要求共产主义者“先联合资产阶级完成他所说的资产阶级革命，回头决不与资产阶级联合执政，而是要利用革命后的政治自由向资产阶级政府提出他们不可能答应的要求而后推翻之”（1848年德国革命期间马克思给德国革命者的信）；为了建设“人人平等”的共产主义“天堂”，马克思号召底层民众“推翻资产阶级”人为的将社会分为三六九等的所谓“阶级”互相内斗仇杀。不知道马克思在得意洋洋的意淫着“无产阶级革命”的时候，是不是还记得他那套“辩证唯物主义”的信条“事物是相对运动着的，在一定条件下可以相互转化”。试问，资产阶级一旦破产，就会变成无产阶级。无产阶级努力工作，也有机会成为资产阶级中的一员。这样两个相互变动的“阶级”一旦对立乃至斗争，会造成怎样的结果，不言而喻。试问，“资产阶级”的判定标准应该是什么呢？<br />
<br />
很简单，无产阶级专政，让无产阶级来判定谁是资产阶级。让无产阶级来做到公平分配。说的比唱的都好听的犹太穷鬼马克思，还在《共产党宣言》中大言不惭的替这种摆明违反'人人平等“的专政政策辩护，他说”在发展进程中，当阶级差别已经消失而全部生产集中在联合起来的个人的手里的时候，公众的权力就失去政治性质。原来意义上的政治权力，是一个阶级用以压迫另一个阶级的有组织的暴力。如果说无产阶级在反对资产阶级的斗争中一定要联合为阶级，如果说它通过革命使自己成为统治阶级，并以统治阶级的资格用暴力消灭旧的生产关系，那末它在消灭这种生产关系的同时，也就消灭了阶级对立和阶级本身的存在条件，从而消灭了它自己这个阶级的统治。“<br />
<br />
听上去还真像那么回事，可是，不要忘记”绝对的权力带来绝对的腐败“。哈耶克在他的旷世名作《通往奴役之路》中告诉我们”任何控制一切经济活动的人也就控制了用于我们所有的目标的手段，因而也就必定决定哪一种需要予以满足和哪一种需要不予满足。”一个掌握了社会命脉的政权，怎可能心甘情愿的在“共产主义社会”到来后，交出政权？要知道“当一个团体取得政治权力之后，它更倾向于保持这种权力，而不是在一定的时候将权力交出――即使这种权力是在革命或建立平等社会的名义下取得。 ”这可正应了那句“名言”你们独裁！可爱的先生们，你们说对了，我们就是要独裁&quot;（毛泽东）<br />
马克思的共产主义天堂“生产力极大提高，物质生活水平极大丰富”可惜，“天堂”里缺了几样东西，有了这些东西在，或许文化大革命就不会发生，赤柬的大屠杀也不会震惊世界。<br />
<br />
“天堂”里缺了什么呢？缺了法律，缺了道德！马克思告诉我们“法律、道德、宗教，在他们（无产者）看来全都是掩盖资产阶级利益的资产阶级的偏见。”这是哪门子的天堂？没有法律，没有道德，这是彻头彻尾的地狱！<br />
<br />
一个没有道德，没有法律，依靠极权的无产阶级专政建立的所谓社会主义，一方面商品短缺，一方面就业难。一方面宣传人人有饭吃，一方面宣传又是不劳动者不得食，否定了财富的积累和传承，也就没有了文明的传承。然后马克思还告诉我们，这是一个“　代替那存在着阶级对立的资产阶级旧社会的，将是这样一个联合体，在那里，每个人的自由发展是一切人的自由发展的条件。”<br />
<br />
这就是《共产党宣言》里的马克思式天堂，共产党宣言是什么？通往奴役之路的金钥匙！在马克思“共产主义乌托邦”里，没有道德，没有法律，没有自由，没有民主。有的只是血腥的“无产阶级专政”，无法无天的暴力革命论，共同贫贱的“公有制”。一个阶级对立的“平等社会”<br />
<br />
