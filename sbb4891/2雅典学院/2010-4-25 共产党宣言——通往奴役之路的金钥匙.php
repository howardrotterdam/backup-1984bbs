所谓份地，就是农奴耕种的土地，这种土地的所有权当然是属于领主的；所谓“公地”...其实也是属于领主的，因为它同样是封建领主制背景下的产物，其“公”对应的是“份”。<br />
在一个领主的范围内，土地分为三类，一类是份地，类似“自留地”；一类是领主直辖的土地，由农奴提供劳役来耕种；最后一类就是那些荒地，称为公地，所有人都可以在上面放牧、割草、砍柴什么的。这三类土地的所有权，都是属于领主的。<br />
由此可见，从资本主义产权关系上看，圈地运动并没有剥夺什么，只不过那个时代本来就没有资本主义产权关系罢了。</div>

							
							
							
							
