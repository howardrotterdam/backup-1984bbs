																											<div id="postmessage_301013" class="t_msgfont">莫伸手，伸手必被捉<br />
<br />
<br />
<br />
今日，上海倒钩一案终于出了初步的结果，政府网站东方网的标题是：上海:坚决依法整治非法营运 坚决禁止不正当取证行为。<br />
<br />
我们不仅仅要学会做除法，还要学会解读政府的官话。<br />
<br />
虽然官话里有百分之九十九是废话，但还有百分之一的屁话，这百分之一决定了政府经过考虑以后的方向，里面往往蕴含了很多的讯息。无论从商从政都要有良好的解读能力。<br />
<br />
从顺序上来说，政府是不甘心的，因为政府强调了要坚决依法整治非法运营，表明了他们咽不下这口气，但是这次的确是栽了，后面那一句坚决禁止不正当取证行为是表示，老子暂时认栽。<br />
<br />
但是，政府会两次栽到同一条河里吗？很遗憾，答案是会的。因为正常人都不大会。但是政府经常同样一条河里要掉好多次，只是在不同的地方以不同的姿势掉进去。当然，永远淹不死那就是他的本事了。<br />
<br />
我认为，这次的事件中，闵行是占了便宜的，因为舆论的导向最后都指向了浦东新区。说不定闵行出事那会儿，浦东正笑闵行呢，哪知古有孙中山断发明志，今有孙中界断指明智。上海倒钩茂盛的地方时闵行和南汇，这次南汇并入了浦东，所以浦东也算是继承发扬了原南汇的光荣传统。文章写到这里，提醒大家，无论是从倒钩数量和次数来说，闵行很可能是老大，勿忘闵行。<br />
<br />
 <br />
<br />
但是这样影响恶劣的事情，为什么市政府的态度从一开始就比较纵容呢，为什么要让这样的事情一再破坏政府的公信力呢？原因之一是一看自己，靠，原来本来就没什么公信力了，也就算了。原因之二，那就是钱。据报道，在闵行倒钩见报以后，闵行区马上刊登了一个来自运管局的工作报告，报告称，在过去两年里，通过这样的方法，罚款了5000多万元，超额完成了上级的指标。<br />
<br />
按照我们的理解，这是很白痴的一件事情，千夫所指的时候还要留人把柄，顶风作案，好比你已经杀了一个人，突然你跳出来说，这两年我其实杀了二十个。但是官员是很蠢的，他们之所以这么写，是在提醒他们的上级，我有功啊。从黑社会的角度来理解就比较容易，就是大哥说，教训一下这帮人，小弟一出马，弄死一帮，出事了。小弟立马说，爷，你要保我啊，是你让我干的啊，你看，我都超额完成了，你让我教训一下，我不光教训了，还弄死了二十个。<br />
<br />
老大一挠头，说，你这个白痴，让你教训一下就好，你弄死人家干嘛呢？<br />
<br />
小弟说，爷，这不弄死，怎么扬你的威风呢？<br />
<br />
老大说，事情弄这么大，我暂时也保不住你，你出去避避风头吧。<br />
<br />
小弟说，爷，你一定得帮我啊，我还帮你多收了5000多万呢。<br />
<br />
老大，算了算了别说了……<br />
<br />
 <br />
<br />
这样大家就好理解各种关系了，是的，那5000多万赃款是让闵行运管洋洋得意政绩，在这件事情上，我认为不是简单的老大出面说一句我的小弟这次没做好这么简单的事情，因为这个不是犯错，这是犯罪。还有专家为倒钩辩护说，美国警察也警察倒钩，他们假装吸毒的去倒钩毒贩子。我们且不说这个比喻有多么愚蠢，但我们做的事情也不是这样的啊，我们干的就是假装吸毒的去倒钩普通人，一看不是就往他手里塞一包毒品，然后立马把人家当贩毒的抓起来。<br />
<br />
倒钩事件里，政府部门涉嫌到非常多的犯罪行为，包括抢劫罪，诈骗罪，诬陷罪，非法拘禁罪，故意伤害罪和组织黑社会罪，而不是执法不当的行为，我认为上海市政府的这个处理态度是在转变事情的性质，引导舆论和公众这只是一起执法不当而已。另外，我提醒一下广大的真正的黑车司机，你们最近要小心了，大家都是在这个生存起来很艰难的城市里混口饭吃，但是按照政府的一贯手腕，倒钩一案过去以后，将进行集中的黑车整治活动，将有更加严密的取证手段和处罚，再加上出租车刚刚涨价，你们可能要不幸，所以要做好准备。<br />
<br />
这件事情的合理处理方式，我认为应该是这样的才合理：<br />
<br />
1，&nbsp;&nbsp;政府改变出租行业的管理方法，降低入行门槛，减免对出租车司机的压迫。<br />
<br />
2，&nbsp;&nbsp;让广大黑车司机成为正规出租车司机。<br />
<br />
3，&nbsp;&nbsp;对运管局进行全面的停业整顿。<br />
<br />
4，&nbsp;&nbsp;追查5000万抢劫款的用处（虽然这钱对于政府来讲很不起眼）将5000万的抢劫款全部退还到个人，哪怕里面有4000万是真正的黑车处罚，一个健全的美好的城市其实是不存在非法运营这四个字的。<br />
<br />
5，&nbsp;&nbsp;对于第一次非法运营的人不做处罚，只做记录，到第二次才实行处罚，这样就可以大大降低甄别失误，如果是好人，就等于告诉他，以后别让人搭车了。<br />
<br />
6，&nbsp;&nbsp;对闵行区政府主要领导进行处罚和撤职。对相关人员论罪处罚。<br />
<br />
7，&nbsp;&nbsp;副市长（包括）以上级别相关领导引咎辞职。<br />
<br />
 <br />
<br />
以上七条其实是在任何一个法制国家里最常见的处理方法，你可以经常看见其他国家地区的领导因为屁大的事情就引咎辞职，但我国的领导哪怕出了天大的事情都还巍然不动，最多就道歉一下完事，原因其实很简单，你忍心法办经常和你一张桌子上吃饭的朋友吗？你忍心法办和你在一张床上嫖娼的兄弟吗？<br />
<br />
所以，解决问题的根本中的根本就是，严禁公务员在一张桌子上吃饭。<br />
<br />
 <br />
<br />
此事如果没有彻底解决的方法，我建议全上海的驾驶员在世博会期间在车上贴上中文英文日文法文大标语告示国外友人：莫伸手，伸手必被捉。<br />
<br />
这是个什么样的世界啊，写下这句话的原上海市市长陈毅肯定也想不明白一个世纪后的上海，明明是你伸的手，为什么被捉的却是我呢？<br />
