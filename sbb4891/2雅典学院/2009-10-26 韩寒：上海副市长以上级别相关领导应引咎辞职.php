效果评价<br />
<br />
　　开放数量和费率管制仅仅30天，出租车经营者的数量就增加了50%，有几家出租车提供的费率已比改革前的政府定价低了10%。无论新老出租车，服务水平均有提高。乘客的候车时间缩短了，投诉也消失了。几乎在一夜之间，司机们扔掉了邋里邋遢的T恤衫，换上了整洁的衬衣，有的还戴上领结;车辆变得清洁明亮，街道上的出租车多了起来。<br />
<br />
　　开放管制两年后，根据印市官方统计，注册的出租车公司从放松管制前的26家增加到74家，出租车总计约500辆，大多数新公司都是只有1～2辆车的小企业。其中，至少40家公司由少数族群人士或妇女开办。随着大量小出租车公司的出现，两家派车合作社也相继设立。这些新成立的小公司大多都加入派车合作社，这样就提高了预约的服务效率。<br />
<br />
　　出租车的平均费率比放松管制前下降了7%，服务水平得到提升。虽然出租车数量比数量管制期间增加了一倍，但管理当局收到的投诉却减少许多。电话约车的平均等候时间从放松前的45分钟缩短为20分钟。此外，巡游服务也给消费者带来了方便。<br />
<br />
　　印市解除数量管制后，出租车和经营者的数量均有增加(新经营者主要是小公司或个体业主)，但其出租车的安全和服务水平并未下降。这与印市保留了适当的标准官职有很大关系。这也是该市与20世纪80年代那些放松管制城市的不同之处。<br />
