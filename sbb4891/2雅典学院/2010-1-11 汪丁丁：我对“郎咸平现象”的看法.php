													<h2>汪丁丁：我对“郎咸平现象”的看法</h2>
						
						
																											<div id="postmessage_425170" class="t_msgfont">最近几年，郎咸平是一种现象，转型期中国社会的现象。我的态度，在各种场合表述过，也被转述过，也引发过不少争议。干脆，一位网友的提醒，我今天就写一篇正式文章，表达我的看法。<br />
<br />
 <br />
<br />
我关注“郎咸平现象”，是因为它典型地反映了中国社会经济深层问题的积累已足以形成“干柴”效应，也称为“多米诺效应”。我曾撰文介绍格莱诺维特的理论，说明这一效应与社会骚乱的关系。更早，我还曾撰文分析了因医疗服务的长期弊端而积累至危险程度的普遍的焦虑。后者，是我们社会的心理疾病。<br />
<br />
 <br />
<br />
郎咸平对中国社会经济的批评，原本与我们的复杂自由主义立场的批评类同。我们不能赞同的，是郎咸平提出的改革方案。他的一系列“救世良方”之更严重的弊端，完全植根于他对政府的盲目信任，更遗憾的是，他从不意识到这一盲目性。<br />
<br />
 <br />
<br />
中国的事情往往是复杂的，例如，许多不同派别的学者可以类同地针砭时弊，如同民国初年的情形一样。关键在于，他们为中国社会开具的药方，决定了他们所属的派别。类似的情形，在今天，典型地，是人人可呼吁“改革”，人人可以“改革”为旗帜。关键是，怎样改革？于是显出流派之争。<br />
<br />
 <br />
<br />
简单地说，骨子里相信“市场”还是骨子里相信“政府”，这是一块试金石。郎咸平政策建议的基本思路，我们坚决反对。坚决反对，是因为，凡是在中国大陆长期生活并深感计划体制之弊端的中国人，大致都对中央集权有所警惕。而郎咸平政策建议的基本思路，客观上恰好是强化或倾向于强化集权的。我读他的文章得到的基本感觉是，他骨子里相信：“乱世用重典”。粗略而言，这一思路有道理。细细品味，难道二十世纪中国的百年乱世用的典不够重？关键在于，什么样的典？谁有生杀大权“用重典”？这才是“中国革命的根本问题”。国共双方，早期都参与了“中国社会基本性质问题”的大辩论，为什么？这算是我给“新政治经济学”读者的家庭作业。在这方面（即“治乱世用重典”），我始终认为，没有哪一个人――包括郎咸平想象中的他自己或任何其他人――能超过毛泽东。毛泽东之所以在全党全军具有“卡里斯玛”权威，主要因为他长期的卓越工作最终成功了中国共产党夺取政权的革命。我们可以容忍毛泽东晚年的许多错误，是因为我们还无法摆脱他的卡里斯玛魅力。<br />
<br />
 <br />
<br />
当代中国，毛泽东“卡里斯玛”权威之后的中国，其弊端之大源头，在道德沦丧与官僚政治，而不在“乱世”本身。前者是本，后者是标。郎咸平政策建议的基本思路不仅是治标而不治本的，而且很可能助纣为虐。<br />
<br />
 <br />
<br />
其次，回到主题，我关注“郎咸平现象”，因为正是这一现象，表明了中国大众心理深层躁动着的激进情绪。假如，让我们想象我们在一年内实行了真正民选基础上的多党政治，那么，这样普遍的激进情绪很可能成为政见最过激的政党的选票。熟悉民初历史的读者，1912年至1918年之间，我们缺少这样的过激政党吗？我立刻想到的是，无政府主义各流派，和保皇党的各分支。这样两极端的激烈斗争，很大程度上解释了二十世纪上半叶中国社会政治生活的残酷性。难道二十一世纪，我们还需要再一次经历这样的激进主义吗？不错，我们的社会记忆，实在太短暂。也因此，我们在复杂自由主义立场上，才必须不断唤醒社会记忆！<br />
<br />
 <br />
<br />
网络民主，不错，我也喜欢网络。但网络民主在中国这样的专断政治格局内很容易沦为民主暴政。因为，反正我们没有其它表达渠道。所以，我们不打算批判网络社会的过激党们，对他们的行为和表达方式（往往是谩骂），我们充分地理解。但难道这样宣泄之后，世界能好些？空想！自我麻醉！看看那些真正经历过社会变革的复杂过程的朋友们的思路吧。例如，罗小朋，他几年来设想的，是建构一个公共知识分子网络平台，通过认真和自由的对话，寻求公共政策的合理基础。小朋的年龄，或许比多数网络激进分子的父母还要大。与这样的老朋友相比，你们不觉得你们的“年轻”被你们浪费了吗？<br />
<br />
 <br />
<br />
郎咸平现象，它反映的是我们大众的普遍的缺乏思考。后者，普遍地表现为阿伦特感到切肤之痛的“平庸之恶”。<br />
<br />
 <br />
<br />
附件：我发表在《财经》2008年1月“边缘”栏目的文章，<br />
<br />
我喜欢原文的标题，虽然有些激烈，但可引发联想：<br />
<br />
“假如你不想亡党亡国……”<br />
<br />
汪丁丁<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;去年，可能是类似事件的激发，我写过一篇评论，标题是“愚民政策与社会动乱”，流传甚广，想必读者共鸣甚烈。我在那篇文章里转述的是斯坦福大学社会学泰斗格兰诺维特教授早年一篇论文所揭示的“社会不稳定原理”――该原理被通俗地称为“多米诺效应”――即社会骚乱往往只需要第一个人参与，然后，由于集体行为可能非常敏感地依赖于个体行为，“集体意识”被这第一个人的行动激活，迅速卷入骚乱，后者于是升级为“社会动乱”……（Mark Granovetter，1978，“threshold models of collective behavior”，《American Journal of Sociology》vol. 83，no. 6，pp. 1420-1443）。<br />
<br />
使得社会骚乱逐步升级的决定性参数被称为“个体从众倾向”，当这一参数的取值超过某一阈值时，该个体可被任一微不足道的事件激怒从而实施过激行为。我们可设想一群个体按照他们从众倾向的阈值的顺序，从低到高排列为一条“骚乱”曲线――阈值最低者最先受到激发参与骚乱。关键在于，沿着这条骚乱曲线，只要阈值最低的个体受到激发参与骚乱，其余的个体将依次受到激发，纷纷参与骚乱。也就是说，这一群体的集体行为的阈值，可由骚乱曲线的最低阈值代表。<br />
<br />
格兰诺维特的论文标题可直译做“集体行为的阈值”。贝克尔和墨菲在新作《社会经济学》中试图解释的也是这类现象，只不过他们引入的核心概念是“社会资本”而不是“阈值”。<br />
<br />
格兰诺维特的理论适用于八类社会情形：创新扩散过程，谣言与疾病传播，罢工与骚乱，政治选举，教育投资，社交，移民，社会心理。显然，这八类重要社会现象与我们今天广泛讨论的“和谐社会”密切相关。<br />
<br />
根据贝克尔的定义，凡影响个体行为并且不显著受个体行为影响的集体行为因素，都可归入“社会资本”概念。由此，贝克尔推演出一项基本原理：群体行为可以非常敏感地依赖于个体行为，以致在该群体内没有哪一个体感受到自己的行为发生显著改变时却可以感受到群体行为发生了显著改变。这一原理十分类似于格兰诺维特在这篇论文里论证的社会不稳定原理。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;可见，假如我们承认格兰诺维特的模型符合我们对社会动乱触发过程的基本观察，那么，我们就应当认真对待这一模型并且认真研究那些触发了每一社会群体内阈值最低的个体的社会因素。通常，一个社会由三类群体组成――中产阶级、上层和底层。其中，中产阶级具有最高的“骚乱”阈值，他们最不愿意有社会动乱。但在转型期中国社会，中产阶级不是一个稳定的群体。于是，心态的包容性取代财产稳定性成为防止社会动乱的重要因素。当民众的心态日益偏激时，社会动乱特别容易被触发。<br />
<br />
我们深知，在目前的舆论条件下，对每一篇文章的作者而言，或许最好的选择是回避讨论这类敏感议题。可是对群体而言，每一个人的这种回避态度最终会导致灾难性的结局。所以，假如你不想亡党亡国的话，你最好还是坦率地发表自己的见解。例如，针对辽宁西丰县进京拘捕记者事件，许多作者已经发表了许多见解。这些见解衬托着一个更根本的见解――自由舆论是社会稳定的基石。<br />
<br />
自由的舆论当然要包含各种我们不喜欢的舆论，难道我们可能只允许我们喜欢的舆论“自由”吗？可笑！荒唐！愚蠢！让我引述1945年3月31日《新华日报》引述过的吴晗先生的一段评论：“目前的事实，是报纸和舆论分了家，舆论被埋没在每一个人民的胸坎中，报纸杂志离开了现实，背叛了人民，孤零零地挂在半空中，不上不下，不进不退，不左不右，不死不活，只作为这时代的一个应有的点缀品罢了。”<br />
<br />
任何一位社会成员，特别是领导干部，例如那位县委书记，在一个价值观与生活方式已经多元化了的社会里，必须学会适应多元的价值观与生活方式。换句话说，他必须学会“宽容”――对异端的宽容。对主流价值观与生活方式的反叛行为，每一社会成员都持有一种宽容的态度。这是一个健康社会的最本质的特点。洛克在他的名篇“论宗教宽容”里有所论证，但更雄辩的论证来自社会实践的历史。宽容的态度导致了政治温和的中产阶级立场，后者构成了社会顶层和底层之间尖锐冲突的缓解区域，有效地防止了社会动乱。<br />
<br />
在一切能够降低社会骚乱的心理阈值的愚蠢政策当中最愚蠢的，是所谓“愚民政策”。这一政策的直接后果就是造成全体社会成员的价值观与生活方式的趋同，并由这一趋同而形成价值观单一的社会网络（即贝克尔所谓“社会资本”的载体）。这样一种政策将把最大多数社会成员改造为“愤怒青年”，让他们具有最低的骚乱阈值，他们将以微不足道的借口参与骚乱，因为他们的头脑不能想象任何更温和的批判手段。<br />
<br />
一位普通记者的报道，无论如何应算是“温和的批判手段”。那些下令拘捕“温和”的干部，其实是在欢迎“骚乱”和“革命”，他们的头脑恰好是他们长期愚民政策的产物。<br />
<br />
所以，转型期中国政府的政策，不仅必须防止严重不公的收入分配，而且必须防止愚民式的宣传。明智的政策是尽量开启民智，让宽容态度成为政治文化的核心内容，鼓励市民公开批评政府――从地方到中央。记住，假如你不想亡党亡国……。<br />
<br />
这篇文稿在“思想库”发表时的标题是：愚民政策与社会动乱</div>

							
							
							
							
