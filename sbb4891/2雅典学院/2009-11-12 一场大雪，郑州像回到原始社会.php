――11月12日早上班小记<br />
<br />
<br />
1.<br />
<br />
11日夜、12日晨下了一场大雪，多年罕见，据报道大雪席卷华北数省。别的城市咱不知道，郑州市却真的像是回到了原始社会――<br />
那种荒凉，那种清新，那种返朴归真的深厚积雪与泥泞……<br />
俺不禁仰天长叹――<br />
这种奇迹容易吗？感谢郑州市政哇，让俺们在繁忙的工作生活之余，徒添了与大自然嬉戏的快乐，锻炼了身体，娱乐了精神，陶冶了心灵，砥砺了精神。<br />
如果允许，俺要给郑州市政送锦旗；<br />
如果只能给那么多先进的政府部门送锦旗，俺首先送给郑州市政部门；<br />
如果“感动中国”有评选政府部门的，俺第一个投郑州市政部门的票！<br />
<br />
2.<br />
<br />
一大早，发现外边下雪了，老婆说开不成车了，送不成孩子了，今天虫虫不上幼儿园了请假吧。<br />
她还想让俺骑电动车带她，俺考虑自己的技术，还是婉言谢绝，老婆只好也不去上班。<br />
俺因为一个急活今天必须做，天上下刀子也得去上班啊，所以，俺毅然决然，脖子上勒条围巾，出门了。<br />
一下楼俺就惊呆了：哇，雪停了，这积雪也太大了！<br />
厚厚地几乎把小腿肚都埋住！<br />
物业人员只在积雪中开了条二十来工分宽的小路，沿着这条小路，俺来到地下停车场，沿路发现，小区道路上积雪没有及时清理，经过车辆碾压，极不好走。<br />
但俺还是推上电动车上路了。<br />
俺社区的物业公司，虽然在俺们跟打官司时，哭诉人手多，管理成本高，但一到事上，就不见人手到哪儿去了。不过，俺充分信任政府可不会像臭名昭著的物业公司那样啊，马路上的情况，一定要比社区好得多。<br />
因此，俺推上了电动车。<br />
事实证明，这完全是一个错误的决定，简直让俺把肠子都悔青了！<br />
<br />
3．<br />
<br />
社区道路根本无法骑，人多不说，到处都是深深的积雪，被碾压的地方，是硬冰凌，是冰凌的沟坎，还有污水。<br />
只能推着电动车，来到西门，也就是花园路上，好不容易啊。<br />
俺在21世纪社区住，要到农业路单位上班。俺不知道，从俺出门的时候起，俺就迈上了一条漫长的不归之路！而从俺推上电动车起，这条不归之路，就更加艰难曲折了！<br />
只见21世纪西门口处，花园路上，车辆乱作一团，冰雪、泥泞，几无插脚之处，如果冒然骑车，不仅鞋子踩在雪水里要完蛋，而且还要冒着和汽车相撞的危险。<br />
俺只好在人行道上推。此处，别提多可笑――积雪的地方阻力很大，给踩得露出地面之处，又像溜冰场，原来地面的地转光滑无比――经过了无数次的开挖，无数次的翻新，无数次的精益求精，路面终于铺上了这种巨滑的地砖，平时看上去很美，闪闪发亮，贵族气质，造价不菲，此时又可作溜冰之用，郑州市替市民考虑得太周到啦！<br />
看着那些行人滑得前仰后合，翻滚不已，俺这个乐啊，连忙取出手机来拍照，可是镜头还没对准，后边的人就开始呵斥，挡道了！<br />
俺连忙行进当中拍照取乐，幸灾乐祸，但自己却也险象环生，摇摇欲坠，并且看到竟有人在拍俺！真是自娱娱人哪，看人出丑并自己出丑，非常快乐，就好像小时候的大雪天一样，只恨没有熟识的朋友，和TA一块到雪地上滚滚去。<br />
<br />
4,<br />
<br />
等俺推电驴得累了，俺发现俺实际上是带了个大累赘！<br />
那些步行者，何等轻松，何等快乐，他们在雪地和冰凌棱子上，摇摇摆摆，好像跳舞一样，尤其是那些女士，更尤其是那些漂亮女士，身材婀娜，姿态优雅，宛若“凌波微步”，又似冰上芭蕾，当然，在完成了一系列高难动作之后，忽嗵一声，四脚朝天，就更让人赏心悦目啦……（这基本上可以证明我是一个坏人）<br />
后退回社区存车，已是不可能，前进！多好哇，多好的锻炼身体的机会！<br />
平时，恁不是整天说没机会、没时间锻炼嘛？一周抽两晚时间，去俱乐部打球，都得经过老婆恩准，好难啊！现在，上班路上也能锻炼，随时随地都能锻炼，机会多难得啊！<br />
感谢老天爷，更感谢郑州市政，给俺创造了这么好一个锻炼身体的机会――偷偷说一声，更感谢给俺创造了这么好的偷看美女摔跤的机会，嘿嘿……<br />
感谢哇！不仅练眼力，你得紧紧盯着哪块有下脚和推车的地方啊，哪块有车辙，没有泥水，没有厚雪，赶快冲过去！否则就被人占领了！<br />
还能锻炼腿力，还能锻炼臂力，你得在滑不溜秋的地面上蹬啊，推啊，锻炼平衡能力，锻炼腕力，小臂力，还有腰力，有时候要全身发力，才能推过去哇！<br />
俱乐部的弟兄们已经看出来啦――太棒啦，这不整个一乒乓球素质专业训练吗？！<br />
大雪后走在郑州市道路上，能增进身体各部分机能。<br />
大雪后走在郑州市道路上，能提高身体的柔韧性、协调性、持久性，能提高暴发力。<br />
大雪后走在郑州市道路上，能锻炼眼里，手力，腕力，臂力，腰力，腿力，脚力。<br />
大雪后走在郑州市道路上，是一种比专业更专业的专业体能训练！<br />
国家乒乓球队如果了解了这一情况，会取消体能训练课，把训练场搬到这儿，一定会把所有队员都训练成王大力。<br />
中国足球队如果能把体能训练场挪到郑州市，大雪过后的郑州市，每天在如此路面上训练一番，不出一周就能平韩灭日，冲出亚洲；走两周，能面欧洲强队；走三周，菜了巴西不在话下；走四周，乖乖，想像不出来啦，大概太阳系都盛不下了……<br />
<br />
5.<br />
<br />
如此浮想联翩，这个高兴啊，这个激动啊，这个感谢啊，在这个专业训练场上，多么幸福啊。奥运会、全运会后，河南体育成绩欠佳，许多人批评郑州市这个排头兵很熊，要是年年都有这种大雪，也就是说大家年年如此锻炼，市民个个都成为运动健将了，何愁郑州体育不出成绩？何愁郑州体育不笑傲全球？<br />
郑州市市政年年都为这种训练计划作了充足准备啊，都怪老天爷！直到今年才下这么大雪！<br />
千万要把这种良好的态势保持下去、万万不可破坏这种全民健身的良好环境啊……俺在心里默默祈祷着……<br />
<br />
6.<br />
<br />
看到北环桥上，被车辆压出一片无雪但泥水横流地面，俺实在是太累了，只好走此处，骑上了车，没办法，看到桥下，那么多群众在雪地里左右摇摆，打滚摔跤，锻炼不休，俺不禁非常自责……<br />
但桥上也是训练场！得躲避那些终于可以撒着欢开的车哇，那些机动车大爷，平时就骄傲得很，加塞、挤道，横冲直撞，好像要锻炼着誓夺世界一级方程式冠军似的。这时，它们就更加兴奋啦，就好像原始社会的野兽似的，平时它们也像野兽，可毕竟有交通规则啊，这时路口的红绿灯完全失效了，没人管了，所以就像原始社会的恐龙，一路疾驶，有的还嗷嗷怪叫（鸣着喇叭），噪音把你耳朵都要震聋了，溅起的雪水哗啦啦四下乱喷，躲避不及，腿脚上车上就会狼籍一片……<br />
俺本来还对俺的800块的playboy皮鞋倍加保护，此时，发现都多余了，脚上腿上，完全都成汽车溅泥水了，而且踩在雪水出，都湿透了，脚上冰凉一片，快冻成冰凌疙瘩了。<br />
感谢主！俺这时想起了圣经出的约伯，越是倒霉，越是受苦，老约伯越是感谢主，赐给他考验，让他更坚强。<br />
俺们无神论者，不信上帝，上帝就是政府嘛，此时俺只想向约伯那样，双膝跪地，双手合什，对天高颂：<br />
“感谢市政！”<br />
您不仅让俺身体得到锻炼，更磨炼俺的意志品质，除了被机动车轧死轧伤，那太悲剧俺不愿意，俺愿意承受们的各种蹂躏，只当是真主市政给俺的考验，们溅了俺左边，俺再把右边靠过去让们溅湿，当们身陷泥坑出不去，或者彼此亲嘴的时候，俺只当是真主市政导演的戏剧，为的是让大家娱乐……<br />
<br />
7.<br />
<br />
真的有车辆陷进雪窝，出不来，就像史前的猛兽掉到了猎人的陷阱……<br />
这时，旁边就会跳出一帮群众，要求推车，他们伸着手指头，索要人民币。<br />
们只好气哼哼地掏钱。<br />
感谢市政啊，给了人民群众创收的好机会，市政和群众心连心，总是增加道路行驶的难度，在大雪天，终于给人民群众创造了无数创收的机会。<br />
真的有车辆亲嘴的，不，亲屁股。<br />
车上两位，跳下车指手划脚，愤怒叫嚣，咋不打起来呀？一圈人都在巴望着来一场拳击赛！只差一场拳击赛就完美了，在这么一个大型奥林匹克运动场上……<br />
交警一时半会也来不了，想想看，今天郑州街头，发生着和将要发生多少车辆接吻的戏剧啊，交警同志你慢慢来呀慢慢来，不要破坏这些唾沫赛或拳击赛，市政搭下大舞台，人民群众免费看戏乐呀么乐开怀……<br />
俺敢打赌，如果说人人都在平凡的生活中盼望点不平凡事情的话，那么12日是郑州市民快乐指数最高的一天，因为发生了多少不平凡的事哇，那些撞坏的车辆，那些摔断的胳膊，都无法冲淡这种快乐，甚至是增添了快乐，让俺们乐不可支，简直乐死了，每看到一次摔跤一次车辆接吻，人民群众就爆发出一片惊叫，然后是一片欢声笑语，郑州市今天算是乐翻了天啦。<br />
俺受D教育多年，不禁暗自思忖，要不要摔一跌，最好把手或腿摔断，来使四周眼巴巴等着看热闹的人民群众愿望得到满足，毕竟，情为民所系，也是俺的心愿。<br />
但俺还是终于压制住了这种美好的想法，因为什么？因为周六俱乐部要打双打比赛，俺摔坏了事小，跟俺配对的风之力先生不倒霉了，风之力不冤死了？这么一想，俺只好小心翼翼地推车，只能对别人幸灾乐祸了，无比遗憾，老天知道，真主般的市政领导也一定知道，俺步行已经不容易，又加上一辆小动物似的需要照顾的电动车，俺是多么有希望摔得鼻青脸肿、像约伯那样高颂上帝啊！<br />
<br />
8.<br />
<br />
不能不说说俺的电驴，可爱的电驴……<br />
它太可爱了，比那些野兽般的机动车辆可爱多了。它既不会像们那样野蛮冲撞，也不会撒娇罢工，一直在受伤似的哼哼声中，陪伴俺的前进……<br />
我亲爱的奥柯玛电驴――<br />
遥想2007年，夏季，一场暴雨，俺像今天这样不知深浅，竟骑了你上班，然后硬着头皮，冲过了积水及膝的北环立交桥下（那儿一下雨就是这样，感谢市政！），冲过了水流得像小型太平洋的经三路，冲过了滚滚黄河一般的农业路。只是到了单位，你才发动不着了，你太懂事了！<br />
俺以为是你坏了。可，并不是这么回事！晾了一天，再发动，你又欢快地启动了！可能是进了水，让你短路吧，水干了，你又泼皮地恢复了工作。<br />
遥想2008年7月12日夜，俺把你放在楼下的地下停车场，一场大雨后，停车场进了大水――同样的感谢市政，俺那边的国基路一下雨就发大水成了“国基河”，是市政让俺免费地成了滨河公寓；感谢比煤疙瘩还黑的开发商，让俺社区的下水管网不合格――大水中，你被冲倒，在90公分的水中淹了整整一夜……<br />
俺以为你完了。可送到维修店，说还能用呢。花钱修了修，你又顽强地上路啦，没一点问题！<br />
遥想去年底，郑州市出台规定，要将电动车限速，挂牌，时速限定在蜗牛爬般的20公里。这不是宣判你的死刑吗？<br />
幸亏领导们事儿多哇，今年大半年过去了，这事儿竟没人提喽！连市人大也不吭声喽，自己通过的法规没人吭气了，人大也不吭气，有时俺想，是老爷大人们，坐着轿车，摇摇晃晃，在舒服的梦里，了解了你的可爱？了解了你的生命力？真主伟大！<br />
你呀，遇到好一点的路――天知道几乎不到路途的千分之一――就勤奋地驮着俺走。<br />
遇到雪，冰，坑，车，你就像弱小的动物，胆怯地躲到一边，但却发出突突的声音，俺能感到你在拼命用力，减轻俺的压力，带着俺前进……<br />
一路上俺想，亲爱的电驴，你太累了，太老了，你该坏了，你坏了吧，俺推着你走。<br />
可你，与俺心有灵犀，你说：哪能呢，在这么好的场地上，俺能放过锻炼的机会吗？俺筋骨结实着类，不信恁走着瞧吧；再说了，俺坏了，不把恁害了，养车前日用车一时哇，俺可不像那些轿车，平时主人对千般娇纵，万般宠爱，关键时就熄火趴窝，亲嘴亲屁股，变成熊蛋，俺要与你一道走完这条路，兄弟加油……<br />
恁说俺能不感动吗？<br />
一路推到、奋进到经三路，俺的电驴始终保持着高昂的斗志，没给俺丢人现眼！<br />
俺不禁感激地说：谢谢好兄弟！<br />
电驴却说：谢谢郑州市政，要不是，要不是的考验，怎见咱人车情深，怎见咱质量过硬，怎见咱兄弟一场！<br />
险些被郑州市封杀的电驴，却无限感谢郑州市……想想，人不如驴。<br />
<br />
9.<br />
<br />
快到了，快到农业路了。<br />
一路上，除了在汽车北站附近看到寥寥清洁工清路，没见任何人干这活，更没有清雪车。<br />
感谢啊感谢，千言万语凝成一句话，感谢市政，恁不是一般的牛X，恁是老母牛找公牛，牛X极了；母牛翻跟头，牛X到顶了。<br />
下雨，恁让郑州市变成游泳池，郑州市民带游泳圈上街真叫一个世界第九奇观。<br />
下雪，恁让全市变成大型溜冰场，滑雪场，体能训练场，免费给市民使用，为全民健身和培养太阳系级伟大运动员作贡献。<br />
不下雨，恁出树，拂之大家头顶的阴霾，让到处享有灿烂阳光。<br />
不下雪，恁修路，把全市变成一个热火朝天、朝气蓬勃的建设工地。来回翻修，不断翻修，年年翻修，月月翻修，天天翻修，天天挖沟，为鸡得屁做贡献，让郑州天天不一样，刻刻有惊喜。<br />
恁是草原牧场上的母牛――真牛X，有条件，牛X，没有条件，创造条件也要牛X。这样牛X的市政，恐怕要冲出地球走向太阳系，走向银河系，走向宇宙，要是有不明小行星冲向太阳系，请郑州市政去挖条沟小行星顺着沟就跑了；银河系哪个恒星瘫缩，请郑州市政去把它翻修一遍，一经的翻修就离不开翻修啦，从此可以继续闹腾很多亿光年（注意，不是年，而是光年，就是说在时间上可能坏得更快，但空间上说不定天琴座的恒星能被修理得一头撞到太阳系）；宇宙再发生大爆炸，请郑州市政去规划，保准出现更多的宇宙奇观，比如恒星乱碰，黑洞中心不是质量最重最重的物质而是像郑州某些柏油路上手一掰就碎的渣子，各个星球无一例外洪水泛滥成灾，加速了生物进化，以至于每个星球上都会诞生人类，地球人再也不用望穿秋水寻找宇宙中的同类，依今天的经验来看，各个星球上都会出现许多冰河期，生物又都会冻死，惟一生存下来的就是郑州市政工作者，们连无处不在、无时不有的舆论风暴都能逃遁，都能历经而浑然无事，强壮无比，在区区冰河世纪，们老人家还不活得像美国大片里的独眼鼬鼠Buck，说不定Buck就是以郑州市政为原型而塑造出来的。<br />
<br />
10.<br />
<br />
现在是晚间7时30分，谢天谢地谢市政，俺写了这篇小文。<br />
俺在发愁：现在咋回家？<br />
市政与俺心有灵犀，俺仿佛听见在说：走回去，勇敢地走回去！这是一个多么美丽浪漫的雪夜啊，恁应该为俺给恁创造的这个步行机会而感到高兴，感到幸福！<br />
俺说：谢谢，谢谢啊。<br />
说：路上看有没有锦旗店上班，买幅锦旗明天送给俺？<br />
俺说：好哇，只要明天仍然能够锻炼身体，仍然有一个好心情。<br />
说：恁在锦旗上准备写啥类？表太过分啊，适当表扬一下就行喽……呵呵呵……<br />
俺说：这样吧，俺喜欢打乒乓球，尤其喜欢打混合双打，混合团体，嘿嘿，恁知道，有美女嘛……表鄙视俺嘛，恁高尚正道表鄙视俺这点低级趣味吆……俺俱乐部对混合比赛的奖品不多，不像恁挖沟修路，一出手都是成万上亿，俺们民间穷俱乐部，就奖俩乒乓球，一白一黄，呵呵，这可是俺心目中的最高荣誉哇，也是俱乐部全体会员心目中的最高荣誉，俺刻苦锻炼，又在恁提供的训练场上饱受磨砺，为的就是在比赛中得到这两个看似普通平常的球。这两个球凝聚着光荣凝聚着心血和汗水，凝聚着顽强拼搏，无私奉献，完全可以作为恁们工作作风的写照！所以，俺送的锦旗上，麻字儿没有，有字，多俗哇，不符合恁们不懈创新、花样翻新的风格，俺的旗上只秀这俩球……<br />
说：咋听着这么不对近儿啊，别是忽悠俺吧？早听说恁灵动有所谓忽悠组，别恁也是忽悠组的？<br />
俺说：天下忽悠是一家，怪不得恁都知道俺。俺这忽悠遇见恁就完蛋了，不值一提哇。还是说说锦旗吧，因为这是混合比赛的奖品，所以这一黄一白俩球，俺们简称叫――“混球”。荣誉归于市政，就像上帝的归上帝，凯撒的归凯撒，这种荣誉，恁可是完全实至名归、当仁不让哦，啊，我心目中的上帝，我心目中的凯撒！我是多么真诚地把你赞美啊，我们的市政，我们的上帝，我们的凯撒……<br />
<br />
完蛋：）<br />
