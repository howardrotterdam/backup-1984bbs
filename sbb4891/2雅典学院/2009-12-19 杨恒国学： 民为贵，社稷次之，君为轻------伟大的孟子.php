																											<div id="postmessage_384535" class="t_msgfont">孟子的民为贵，社稷次之，君为轻这句话,在2000多年前的时代,确实具有光辉先进的一面.可是,这种思想最大的缺陷在于,它只能转化为道德约束------国王可以用这话来勉励自己,臣子也可以用这话来提醒国王.但是,它并不具有强大的约束力.国王白天可以把它高悬于庙堂之上,晚上可以用它擦屁股.<br />
与孟子同时代的西方先贤西塞罗,他的思想集中在法律方面.他寻找的是人性的本源与自然法-----即一切法律(包含宪法)都必须符合自然法.(这是司法复审最初的雏形)法律不是由国王或者大多数人民制定了就可以成立的.<br />
我认为这就是东西方文化最初的水分岭.我们的先人强调的是用思想和道德来约束权力,而西方则是用法律来约束权力.<br />
我们已经用2000多年的历史反复证明过了,儒家学说对限制君权所提倡的东西其实是一种乌托邦.把它用来修身养性还行,但绝不可用来治国平天下.<br />
