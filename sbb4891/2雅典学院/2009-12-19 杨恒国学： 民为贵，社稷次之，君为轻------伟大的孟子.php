法的。民主体制从根本上否定非法政权形式，自由公正的选举是获得管理国家权力<br />
的唯一合法途径。<br />
&nbsp; &nbsp; （四）两院议会制度和内阁制度的确立。实行以国会参、众两院为国家最高立<br />
法权力机构的议会制度，从根本上奠定国家法治化的基础。两院议会制将废止任何<br />
形式的人治，严格执行法治。根据各党派在议会席位的多少，组成责任内阁，依法<br />
行政，可以真正推动议会民主、政党政治的实现，有效避免个人独裁的出现。<br />
（五）独立司法体制的确立。建立完善的现代独立司法体系，确保司法的独立<br />
性与公正性，使法治化在司法层面上得到坚强的捍卫。设立“宪法法院”，将任何<br />
