<br />
从政治观念上，孟子依然在君主专制主义框架内。这点从他的若干关键的断语中可以求证：<br />
<br />
&nbsp;&nbsp;其一，他主张君权神授。他赞成《书》中所说的“天降下民，作之君，作之师。”由于王位是天授的，因此王位不能私自处理，“天子不能以天下与人”。因此他反对私相禅让。当时燕王哙把王位传给了子之，他认为燕王哙超越了权限，禅让必须有天意：“匹夫而有天下者，德必若舜禹，而又有天子荐之者，故仲尼不有天下。”大意是，道德必须像舜禹那样，而且还要有天子的推荐，才能为天子。孔子虽然是圣人，由于没有天子的推荐，所以不能得到天下。禅让就其把权力传给贤人比传给家人而言，有其合理性。但孟子说的禅让乃是指定接班人制，仍属于君主专制体系中的东西。<br />
<br />
&nbsp;&nbsp;君权神授是中国古代帝王的合法性的支柱理论。不管朝代如何更替，新登基的帝王都把自己视为天命选择。<br />
<br />
&nbsp;&nbsp;其二，他赞成孔子说的“天无二日，民无二王”。传说尧把天子之位让给舜，尧死后，舜为尧尽三年之孝，孟子认为这是违背礼的大禁，“舜既为天子矣，又帅天下诸侯以为尧三年丧，是二天子矣”。他认定天子只能是现实的，而且只能有一个，这是绝对的制度。<br />
<br />
&nbsp;&nbsp;其三，“无君无父，是禽兽也”。这是反对杨朱和墨子时放的重话。他说：“杨朱为我，是无君也；墨氏兼爱，是无父也。”这就是“无君无父，是禽兽也”。再进一步，杨、墨之论的后果是“率禽兽食人，人将相食”。孟子无限上纲，往死里整人，杨朱的“为我”并不侵犯他人的利益，杨朱主张君主要少干涉，并不是无君论。墨子讲兼爱只是主张对别人的父母应像对自己的父母一样孝敬。孟子认为面对君主不能讲“为我”，必须把君主放在首位。 <br />
<br />
&nbsp;&nbsp;其四，他的整个政治思想都是在追求“王”、“王道”，并把一切寄希望于王。孟子认为历史的进程是一治一乱。治是“圣人”、“圣王”之功，乱是“暴君”之过。君主暴虐则民背离，君主行仁政则民顺从。孟子认为君主的品质是维系天下的纽带，君主“身正而天下归之”。<br />
<br />
&nbsp;&nbsp;对孟子的上述说法，我们应作两面观。一方面，他向君主提出了极高的要求，由此对暴君进行了抨击。另一方面，在向君主提出道德要求的同时，又把天下的命运系在了君主的脖子上。孟子斥责当时的君主是率禽兽食人之辈，但孟子教导人民，等着吧，“五百年必有王者兴”！<br />
<br />
&nbsp;&nbsp;其五，他自己宣称，他是最“敬王”的。有记载说“孔子三月无君，则皇皇如也”，他认为合乎情理，因为“士之失位也，犹诸侯之失国家也”。<br />
<br />
&nbsp;&nbsp;其六，说庶民失去人性。他说：“人之所以异于禽兽者几希，庶民去之，君子存之。”意思是说，人与禽兽的差别就那么一点点（即他说的人性善的原始因素――“不忍人之心”），庶民都丢掉了，君子保存下来了。他为了论证等级贵贱的合理性，一反自己的人性相同的观点，这与他说的人都是“同类”以及“民为贵”相去何远！<br />
<br />
&nbsp;&nbsp;这六条都是社会结构的制度硬件。他提出的仁政对民众很多同情心，但这属于政策性的软件。把硬件与软件结合起来，可以说他的君主专制主义有相当的柔性，我名之曰“柔性的专制主义”。孟子的柔性专制主义比同辈人要高一筹。但不能因此说他不是君主专制主义。他说的“民为贵”不是民主制，而是说民的背向决定国家的兴败，“贵”说的是名义上的重要性，并没有提供任何实际的制度保障。描述的是一个国家，君主不行了可以闹“革命”换一个，而怎么能将老百姓换掉呢？其“贵”、“轻”只是在这个意义上说的，而并非要废除君民等级专制，给百姓切实的权利保障。</div>

							
							
							
							
