省都享有平等的地位和相同的权利。联邦省、区不得以任何方式破坏联邦的统一，<br />
联邦中央政府依照宪法所赋予的权力维护国家领土完整与主权统一。<br />
&nbsp; &nbsp; （八）监督国家权力体制的确立。所有政府官员和国会议员、法官都是人民的<br />
公仆，他们应始终处于人民的监督之下。权力来源于人民，必须向人民负责。当人<br />
民发现有不称职的官员，人民可以通过法定程序予以罢免。<br />
