<br />
杨恒国学： 民为贵，社稷次之，君为轻------伟大的孟子 <br />
<br />
孟子（约公元前372年―前289年），姓孟，名轲，字子舆。战国中期邹国（今山东省邹县东南）人。孟子是我国著名的思想家、政治家、教育家，他是孔子学说的继承者和发扬者，是儒家最重要的代表人物之一，被后人尊为“亚圣”。<br />
<br />
孟子出生在距孔子（公元前551年―前479年）之死大约百年左右，相传授业于孔子之孙孔常ㄗ铀迹┑拿湃耍他自己曾声称平生“所愿，则学孔子也”,并以“孔尼之徒”自居。<br />
<br />
孟子与孔子相仿，也曾游历诸侯各国，如齐、滕、宋、魏、鲁等国。一度担任齐宣王客卿。虽然孟子比孔子游历的境遇要好得多，虽其主张不被采纳，但孟子倍受王者之礼遇，或礼之以王师，或馈赠以重礼。不过对孟子而言，其所重者绝非这些虚华之表，而最重“仁政”与“王道”的实施。他以“仁政”与“王道”不厌其烦地教导那些顽固不化而又自私自利的君王，因此，他的主张在那个时代最后必然是归于落空。<br />
<br />
然而，孟子是位伟大的乐观主义者。当世不行，孟子诉诸于后世。孟子遂退而教学著书。在教学中，孟子得到了极大的慰藉。尝谓君子有三乐：“父母俱在，兄弟无故，一乐也；仰不愧于天，俯不怍于地，二乐也；得天下英才而教育之，三乐也”。<br />
<br />
晚年，孟子与弟子万章等编著了《孟子》一书，全书共《梁惠王》、《公孙丑》、等七篇，该书是“拟圣而作”，即吸收了《论语》的精华，也接受了《大学》、《中庸》的特点。全书共35000字，说理精辟，文字流畅，语言形象，睿智而犀利，全书翔实记载了孟子的思想、言论与事迹。<br />
<br />
如果说孔子学说奠定了中华民族的社会伦理的基础价值标准，那么孟子学说则是孔子学说在政治领域的扩展。孟子学说奠定了中华民族的政治伦理的基础价值标准。<br />
<br />
孟子学说的光明与伟大在于他那发自人性根本与人类终极关怀的浩然正气。他那“民为贵，社稷次之，君为轻”的政治伦理即使在今天仍然光彩夺目。他在2000多年前，锐利而明确地指出了国家与政府存在的目的，他的民本主义思想中内涵着民主主义的基础――他将“人权―主权―治权”以及“人民―国家―政府”的政治伦理进行了清楚而正确的表达。在君权肆虐的时代，这只有孟子这位拥有绝大道德底气的、最伟大的思想家才能做到的。<br />
<br />
孟子是伟大的，他不仅是一位伟大的智者，亦是一位为民而鸣的圣者。仅从专制帝王朱元璋对孟子学说的刻骨仇恨，就可以看到他的学说是多么的伟大。<br />
<br />
一方面，孟子作为中华道统的发蘖人为后世所推崇，而另一方面，他也受到独裁者朱元璋之流的恶意而冷酷的诋毁。因为孟子伟大，所以他站得高，所以无论哪个方向的风都不免遭遇，而不管风来自哪个方向，孟子永远屹立在伟人所应有的高度上。<br />
这就是我们必须仰视孟子的原因，也是我们必须重新理解孟子的理由。</div>

							
							
							
							
