切都必须以立法形式予以确立并加以贯彻。<br />
（七）以地方自治为基础的联邦体制的确立。以地方自治为基础的联邦体制是<br />
各地方自治的联邦省、区组成的统一联邦制国家。每个联邦省、区在维护和支持国<br />
