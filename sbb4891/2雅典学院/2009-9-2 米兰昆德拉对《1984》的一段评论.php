													<h2>米兰昆德拉对《1984》的一段评论</h2>
						
						
																											<div id="postmessage_222945" class="t_msgfont">这一卡夫卡式的诗让我从对立的方向想起另一部小说，它也是个关于被捕和审判的故事：奥威尔的《一九八四》，一本几十年中作为反专制主义专业人员的长期参考书。在这部想作为一个想象中的专制社会的可怕画像的小说中，没有窗；那里，人们看不见少女和她的盛满水的水罐；这部小说严密地向诗关闭；小说？一部伪装成小说的政治思想；毫无疑问是清醒的、正确的，但是被它的小说的伪装所歪曲，这个伪装使得它不准确，只近乎大概。如果说小说的形式模糊了奥威尔的思想，反之，这个思想是否给了小说一些东西呢？它是否照亮了社会学与政治学都无法进入的神秘之地？没有：境况与人物在其中像一张告示一样平淡。那么它是否至少作为推广好的思想而有一定理由呢？也不是。因为被做成小说的思想不再作为思想而运行，而恰恰是作为小说，在《一九八四》中，它们是作为差的小说，带有一部劣质小说所能运用的恶劣影响。<br />
<br />
　　奥威尔的小说的恶劣影响在于把一个现实无情地缩减为它的纯政治的方面，在于这一方面被缩减到它的典型的消极之中。我拒绝以它有益于反对专制之恶斗争的宣传作为理由而原谅这样的缩减，因为这个恶，恰恰在于把生活缩减为政治，把政治缩减为宣传。所以，奥威尔的小说，且不说它的意图，本身也是专制精神，宣传精神之一种，它把一个被憎恨的社会的生活缩小（并教人去缩小）为一个简单的罪行列举。<br />
<br />
　　共产主义结束一、两年之后，我和一些捷克人交谈，我听到每个人讲话里都有一种成为仪式的说法。所有他们的回忆，所有他们的思索都用这个必不可少的开场白： &quot;四十年可怕的共产主义之后&quot;，或&quot;可怕的四十年&quot;，尤其是&quot;失去的四十年&quot;。我看着说话的对方：他们既没有被迫移民，也没有进监狱，也没有丢掉工作，更没有被歧视；他们都在自己的地方生活，在自己的公寓、自己的工作中，都曾有他们的假期、友谊、爱情，他们说&quot;可怕的四十年&quot;，便把他们的生活缩减为唯一的政治方面。但是，即使是过去40年的政治历史，他们难道真的是作为一个唯一的与恐怖无区分的整块而经历的吗？他们有没有忘记那些年代：看弗尔曼（FORMAN）①的电影， 读哈巴尔（HR ABAL）的书，出入那些不随潮流的小剧场，讲几百个笑话，并在快乐中嘲笑政权？如果他们都讲可怕的40年，那是因为他们把他们对自己生活的回忆奥威尔化，他们的生活在后来，在他们的记忆和他们的脑袋里，失去了价值或甚至干脆被取消（失去的四十年）。</div>

							
							
							
							
