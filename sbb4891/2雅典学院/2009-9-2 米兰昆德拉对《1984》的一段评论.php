&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;但事不尽然，本世纪上叶固多前述的惨例，下叶，却颇不乏后列的雅范；天空海阔，志足神旺，旧阅历得到了新印证，主体客体间的明视距离伸缩自如，层次的深化导发向度的扩展。这是一种带根的流浪人。昆德拉带根流浪，在法国已十多年，与其说他认法国为祖国，不如说他对任何地理上的历史上的“国”都不不具迂腐的情结。 <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;昆德拉在法国不以为是异乡人,意气盎然地认定捷克千载以来本是欧罗之一部分，这是自在的，那末捷克的现状并非不是自在了。所以他会觉得在布拉格反而比在巴黎更有失根之感。此话总该由他说，说得兄弟们相视莫逆而笑。然后，他用捷克文写小说，最熟悉的事物用最熟练的文字来表现。流亡作家以中年去国者为佳，昆德拉的经验、想像全渊源于波希米亚、布拉格。 <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;什么是“布拉格精神”？有直接的或间接的诠释吗？《城堡》、《好兵帅克》谅必就意味着这种精神。说是对于现实的“特别感觉”（出奇的敏感吧）说是持“普通人”的观点，站在下层，纵观历史（仰视的，倒过来的鸟瞰）。 <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;说是“挑战性的纯朴”（如果作“纯朴的挑战性”呢，即原生的反弹力）。 <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;又说所谓“布拉格精神”反具有一种“善于刻划荒谬事物”的“才华”（那是多么可喜）。 <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;又说还有一种“无限悲观的幽默”（那就真是可钦可爱之极了） 。这些，谁说的？米兰.昆德拉。他几乎是在说他自己。 <br />
算来一百多年了，左、右、左派、右派，左而右之，右而左之，左中右倾，右中左倾------ <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;昆德拉说：“在极权主义里，没有左右之分的”。这是一则不妙而绝妙的常识。 <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;大家可以基于此则常识而更作谠论，无奈形的绕转依旧不如穷尽，昆德拉这样一句话，就显昨如雷灌耳了。以“无限悲观的幽默”来对待，那是昆德拉私人的选择。所幸者“布拉格精神”非昆德拉之独具，亦非布拉格之特产，任何时代的任何地域，都有少数被逼成的强者，不得不以思索和判断来营构生活，<strong>当一代文学终于周纳为后世的历史信识，迟是迟了，钟声不断，文学家免不了要担当文字以外的见证，如果灾难多得淹没了文学，那末文学便是“沉钟”。极权主义最大的伎俩，最叵没而可测的居心是：制造无人堪作见证的历史。上帝是坐观者，也从不亲自动手敲几下钟。文学家就此被逼而痛兼史学家，否则企待谁呢。 </strong><br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;压迫，会使文艺更严肃更富活力――这个罗曼蒂克的论点，促成许多俊彦牺牲到没有什么再可牺牲为止，相等于梦中死去。昆德拉知道暗里传阅手稿的年代不会造成文化昌盛期。一九六八年坦克滚进布拉格，“捷克文学”全部查禁，聋、哑、盲、捷克只存在于地图上，地球仪上，一块蝙蝠形的斑迹。 <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;政治教条的首功是：强定善恶，立即使两者绝对化，抹掉中间层次，无处不在的厉虐性构成了。这还只是一重奇妙，更有另一重奇妙紧接而来；人们在俯首听令时，某于徇从最简明易行的令，宗教早就试验了这类庶民的心理取向。贯彻一种酷烈的意志，以采用几个字，两三句烙印鲜明的话最能生效，最富引诱力。 <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;昆德拉看到历史实验室是中欧：一个帝国的覆灭――几许小国的再生――民主――法西斯――德军的强占杀戮――俄军霸据、持异见者遭放逐――理想社会的一线希望――希望的熄灭――昆德拉兄弟们的决然去国------对于人，在这样的历史遭遇中活过来，而正在活下去的人，昆德拉看得发怔，人可以如此孜孜吃吃苟且营生，文学，比“人”更精炼强韧的“文学”，却窒息而死。 <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;奥国的hermanubroch对昆德拉说了句悄悄话：“作家唯一的道德是知识”，听者一惊而笑，他想，然而怎样的文学作品才有存在的理由和价值？该是彰显人类的尚未昭露过的生命的那些篇章。“宣扬真理”，“呈示真理”，昆德拉到为文学家的能事是“呈示”不“宣扬”――他算是冷静了，再冷静下去 ，便见“真理”只供“呈示”无可“宣扬”，唯有被呈示时是纯粹的、一经宣扬便变质的，才可能是真理。文学家在“宣扬真理”这番历时以千年计的繁浩剧情中几乎将文学湮没 ，而 “呈示真理”则已经差不多全是重复重复，徒以呈示的手段为炫耀，所以，再冷静下去，悄悄话也将寂然无闻，不过这毕竟为时还早，文学家之间还有一惊而笑的机缘在。 <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;要说“自然生活”，就涉嫌“理想主义”，仅管理想主义已含羞带愧退远了，剩下的挂念仍然是“怎样才能比较自然地生活”，人类可怜以只求各留一份弹指欲破的隐私，有隐私，就算自然。 <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;“隐私”、“自然生活”，昆德拉乐谈的一而二、二而一的话题、“任何揭人隐私的行为都该受天鞭挞”，谁来鞭挞呢。“隐私”原本不成其为“权利”，当它受到邻人般的警探的般的邻人昼夜作践时，“隐私”才反证为神圣。因此，一旦到了争隐私的时候，必是万难3拥有隐私了。而专以摧残隐私为能事、乐事者，却看准被虐者的弱点，久而久之的作践，使人丧失和生活的界范，再久而久这就泯灭了私生活的意识。 <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;“没有隐私，爱情和友谊将是不可能。”昆德拉在赛纳河畔说这话是有深意的，在坦克的履带下，三复斯言也等于梦呓，新的野蛮以极权、官僚、武力为特征，步步袭毁“自然生活”，举凡“严酷”，皆“轻率”出之，昆德拉认为“轻率，是莫大的罪过”，到了“自然生活”破坏复合使人失去“私生活”的意识时，一切更其轻率得不觉其轻率，“无限悲观的幽默”也棘手于架构文学了――中古的“野蛮”密布遏制止异化的特殊功能。至此，信念转为：轮回即使状如中断，实未中止，运行“野蛮”与“文明”的消长的仅是轮迥的诸律之一律，此一律始终受诸律的制约。 <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;“轮回观念，”怎会是由尼采启示的呢，这个古老观念经尼采重提时失去了宗教幻想，便赤裸直接得使哲学家们大感困扰――它的无处不在的威协性，逼使昆德拉作成其生涯，由此联想到尼采之为尼采，他的文学家身上发生的亲和力，往往大于对哲学家的影响，历历可指的是；凡在理念上追踪尼采的那些人，稍后都乏而离去，莫知所终，而因缘于品性气质，与尼采每有冥契者，个个完成了自己的风范。昆德拉是不孤独的。带根流浪人，精神世界的飘泊者，在航程中前前后后总有所遇合。一个地球仪也够了。</div>

							
							
							
							
