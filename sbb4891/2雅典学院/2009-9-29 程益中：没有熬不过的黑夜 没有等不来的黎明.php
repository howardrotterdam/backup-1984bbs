													<h2>程益中：没有熬不过的黑夜 没有等不来的黎明</h2>
						
						
																											<div id="postmessage_262625" class="t_msgfont">――《南方都市报》执行总编辑程益中在经营委员会2004年新春动员大会上的讲话<br />
<br />
&nbsp; &nbsp; 【按】这是原《南方都市报》总编辑程益中当年的一篇内部讲话，当年曾经在网络上广为流传，演讲中陈述的风险，很快降临到了自己头上。印象中，2004年3月19日，益中出差四川途中被广州方面派员异地拘捕；6月15日，原《南方都市报》总经理喻华峰被广州东山法院一审判处有期徒刑十二年（上诉后被广州中院改判八年），同一天，我代理的《中国改革》杂志社被诉诽谤案在广州天河法院开庭；8月27日，《中国农民调查》案庭审结束当天，“罪行”更重的程益中竟被广州检方依法“不起诉”释放出狱，回到他所谓的“丰衣足食的猪圈”。2005年8月，我和许志永受托作为喻华峰的辩护人，代其向最高法院提起申诉。2008年年初，喻华峰坐满四年后获减刑出狱，但申诉案仍无丝毫回音。看来路正长夜也夜正长，只盼广州政法委书记张桂芳同志早日退休，“南都报案”或可峰回路转。<br />
<br />
&nbsp; &nbsp; 浦志强 2009年9月28日<br />
<br />
各位同仁，战友们： <br />
<br />
&nbsp; &nbsp; 这个冬天特别冷。大雪压青松，悬崖万丈冰。每逢佳节倍思亲，遍插茱萸少一人。一场史无前例的浩劫，把我们和南方都市报推向地狱深渊。面对这场灾难我们如何自处？南方都市报向何处去？在焦灼和煎熬之中，我们除了要经受无边无际、无穷无尽的苦难；更要痛定思痛，梳理凌乱的羽毛，舔干伤口的血迹，透过迷朦的泪眼，穿越乌云飞度的现实，洞察阳光灿烂的未来。 <br />
<br />
&nbsp; &nbsp; 一、如何看待这待灾难？ <br />
<br />
&nbsp; &nbsp; 1、从世界一切伟大事业的成功经验来看，这场灾难的来临具有极高的发生概率。我们干的不是平庸的、混饭的活计。我们干的是惊天动地的伟业。我们向世人宣告过：南方都市报要从强大走向伟大。试想一下：伟大是那么容易担当的吗？！温室中能够长成参天大树吗？！通往中国报业颠峰的道路难道会是平坦的吗？！天降大任的同时，也必降大苦大难。我们既然选择了卓越，我们就没有理由拒绝挫折。世上没有一帆风顺的伟业，只有沿着陡峭山路不停攀登的人，才有希望达到光辉的顶点。责任感使我们出类拔萃。出类拔萃是我们成功和卓越的依据，也是我们受苦受难的根源。木秀于林，风必摧之。快乐总是平凡的，痛苦总是伟大的。越堕落越快乐，越高尚越痛苦。我们所有的作为就是和平庸划清界线。我们所有的价值就是反抗人们骨子里的惰性和奴性。我们探索的是一条别人没有尝试过的道路。我们是当代中国报业当之无愧的先锋。我们这种自命不凡、特立独行的理想主义作风与丑陋不堪、肮脏无聊的社会现实之间，势必形成一触即发的张力。这场灾难便是张力释放的一种方式。受伤总是难免的。 <br />
<br />
&nbsp; &nbsp; 2、从南方都市报与个别权势人物冲突的种种迹象表明，这场灾难蓄谋已久、势在必发。其实，早在2003年六七月间，孙志刚案接近尾声之时，针对南方都市报的有罪推论和掘地三尺的大规模排查就开始了。从那时起，几乎所有与南方都市报有过大宗业务来往的广告公司都被勒令交代与南方都市报相关的违法犯罪问题。有人磨刀霍霍，毫无顾忌地向全社会公开宣称，一定要利用手中掌握的机器，好好收拾收拾南方都市报这帮小子。有人不得不奉命行事。这已是广州城内公开的秘密，坊问谈论已久的话题，可谓司马昭之心，路人皆知。那时候，我和我的一位战友曾经套用邓小平的话说：这场风波迟早要来。我们已经做好了准备：为了国家进步、社会发展、人民幸福，我们遭受一点委屈和苦难也是值得的！如果没有无数仁人志士抛头颅洒热血，我们就没有幸福的今天。人类在追求真善美的过程中，会不断付出高昂的代价，甚至生命！但人类从来都没有因为害怕代价而拒绝真善美。 <br />
<br />
&nbsp; &nbsp; 3、既然灾难迟早要来，但早来总比迟来好。早来，我们还输得起，我们还来得及，我们还不至于无力自拔，我们还不至于前功尽弃。这就好比万米赛跑，起步没多久就摔跤，总比快要冲刺的时候摔跤要好办得多。我们还得来及追赶那些跑在我们前头的人。我们还有大把青春，我们还有大把明天，我们将要干成的事业绝对远远大于我们已经干成的事业。我们还年轻，摔一跤无非鼻青脸肿，还不至于象老迈之人摔跤就一命呜呼。我们现在庆幸的是，我们还没有被成功和胜利的喜悦冲昏头脑，我们还没有居功自傲，我们还比较清醒。我们好在还没有在违法犯罪的道路上泥足深陷。我们的问题只是改革过程中无法回避的问题和经验不足的问题。我们庆幸的是我们没有大的问题，否则我们这次就万劫不复。这场风波来了，给我们当头棒喝，给我们敲响警钟。让我们还来得及完善企业管理各种规章制度、程序和手续，我们还来得及无懈可击。这总比我们罪孽深重的时候才受到审判要幸运得多。 <br />
<br />
&nbsp; &nbsp; 二、如何看待我们自己？ <br />
<br />
&nbsp; &nbsp; 1、无论将要发生什么事情，我们都不要放弃我们曾有的理想和信念。南方都市报捍卫国家、服务社会、效忠人民的理想无罪！南方都市报还原真相、追求真理、奉献真情的信念无罪！理想不能倒，信念不能垮。理想和信念乃是寒夜里手中的火炬、天上的北斗。我们必须志存高远，神思冥想，上达天听，感念天空、大地、河流、原野等一切辽阔的事物。我们不能够让爱我们的人和我们爱的人失望；经此一役，我们不要自抛自弃、自轻自贱，我们反而更要自尊自爱、自立自强；我们更应该握紧我们的理想和信念。 <br />
<br />
&nbsp; &nbsp; 2、无论将要发生什么，我们都不要迷失南方都市报的价值观。我们有理由自豪：南方都市报是一张经得起历史考验的、有存在价值的报纸。南方都市报的价值观就是这个社会主流的价值观，南方都市报的方向就是这个国家报纸应有的方向。南方都市报代表的是先进的生产力、先进文化的发展方向和大多数人民群众的利益。南方都市报是实践\\\\\\\&quot;贴近群众、贴近实际、贴近生活\\\\\\\&quot;的三贴近原则当之无愧的楷模。广大人民群众已经在心中为南方都市报树立了一座丰碑。广大人民群众对南方都市报满怀期待。南方都市报的存在是我们这个社会生活更健康、更文明的体现，也为我们这个社会更合理、更公平增添了保证。对南方都市报容忍是开放社会和民主政治应有的同时也是最起码的姿态。 <br />
<br />
&nbsp; &nbsp; 3、我们大量的正面宣传和表扬报道尽管为当权者\\\\\\\&quot;帮了不少忙\\\\\\\&quot;，但我们极其少量的舆论监督和批评报道也给他们\\\\\\\&quot;添了不少乱\\\\\\\&quot;。现在的问题就在于：帮忙是天经地义，理所当然；而所谓的添乱则要天诛地灭，赶尽杀绝。在这个方面，既有我们如何提高舆论监督的水平和批评报道的艺术的问题；更有一些当权者如何建立与媒体打交道新模式和养成接受舆论监督的好习惯的问题。我们只有一些教训需要吸取，但是我们可以问心无愧的说，对于我们的成绩来说，我们的缺点和不足是微不足道的。这当中，我们应该明白，合作与对话是媒体和公众人物应有的关系。人民公仆也应该明白，和媒体合作与对话，就是和自己的主人--人民合作与对话，因为媒体是一座桥梁；不管你贵为巨星还是贵为天子，你都应该明白，不经过这座桥，你就不能抵达人民心中。 <br />
<br />
&nbsp; &nbsp; 4、我们存在的问题，不是报业改革出了位、出了问题，而是报业改革不深入、不到位的问题。我们面对的问题是，报业在中国还是一个怪胎：报业作为一种产业，当然具有一切产业的市场属性、价值规律和经管法则；但是，报社在中国，机关不像机关，事业不像事业，企业不像企业，人不人鬼不鬼。当查税的时候，报社就是企业；当投保的时候，报社就是事业；当整人的时候，报社就是机关。我们现在的困扰对于企业来说早就不是什么困扰了。经此一役，我们更要铁心搞改革，我们的问题是，机关、事业和企业弊病我们全了，好处我们都占不了。我们的改革就是要融汇机关、事业和企业的长处和优点，将南方都市报的体制改革进行到底。要尽快把南方都市报变成一个高度法制化的现代企业，彻底摆脱人治型企业人亡政息的宿命。我们的经营队伍，是全国最优秀的报业经营队伍。这一点已经被举世公认。我们没有理由动摇对我们经营理念和成绩的信心。我们只是大的企业体制框架不行。我们原有的经营观念和游戏规则不但不能抛弃，不能改变，反而更进一步地强化、进一步推进，我们要把我们以前所取得的经营理念和游戏规则纳入法律的框架之内，使它受到法律保护，而不是受到法律的打击。 <br />
<br />
&nbsp; &nbsp; 三、如何面对我们的未来？ <br />
<br />
&nbsp; &nbsp; 1、南方都市报2004年报纸品牌将保持相当的上升势头，各项经营指标将保持25%-30%的增幅。没有什么力量能够阻挡人民对南方都市报的热爱。这将是谁也无法回避的事实。这张报纸已不仅仅是专属我们的一份事业，更是推动社会进步的一支重要力量。2004年，南方都市报将按照既定的方向继续前进。全年报纸日均发行量将保持在150万份以上，广告额将突破16.5亿元，纯上缴利润将达到2.5亿元。2005年，南方都市报将迎来历史上第一个高峰。这一年，南方都市报的单日发行量有望突破200万份，全年广告额将突破20亿元，纯利润将达到5亿元；南方都市报将毫无争议地登上中国报业排行榜第一的位置。 <br />
<br />
&nbsp; &nbsp; 2、南方都市报系2004年将继续稳健扩张：新京报2004年日均发行量将达到50万份，广告额有望达到4亿元；2004年，南方都市报系中还将增加至少1家日报和2家杂志。2005年，南方都市报系将发展拥有4家大型综合类日报，2家周报，3份杂志的…… <br />
<br />
&nbsp; &nbsp; 3、南方都市报自身发展和报系扩张，将为中国大批有理想、有责任感、有专业素质和职业色彩的年轻报人提供服务社会大众、实现人生价值、施展知识才能的最好平台和最佳机会…… <br />
<br />
&nbsp; &nbsp; 各位同仁，战友们！我也要告诉大家，我们面对的形势非常严峻，现实残酷无情。我们的事业正在经受前所未有的考验。南方都市报正在经受血与火的洗礼。我们不能够保证每一位出发的战士都能归来。我们要学会在炮火连天的战场上，坚守内心宁静的阵地，于狂乱纷披的枪声中，捕捉子弹飞来的方向。我们恳求大家不要在南方都市报最困难的时候离开南方都市报。这既是一种道义，也是一种策略。我们希望大家即便要离开也要选择南方都市报最风光的时候离开南方都市报。 <br />
<br />
&nbsp; &nbsp; 各位同仁，战友们！没有熬不过的黑夜，没有等不来的黎明。还是那句话--没有什么力量能够止得住时间。没有什么力量能够止得住南方都市报这列呼啸而来的战车。只要我们敬畏时间，我们就能够赢得历史；只要我们敬畏民意，我们就能够赢得空间；只要我们敬畏科学，我们就能够赢得真理！只要我们团结起来，发挥智慧，拿出勇气，挥洒汗水，聚精会神办报纸，一心一意搞经营，迎接我们的就不仅是枪炮和污水，也会有鲜花和美酒！ <br />
<br />
&nbsp; &nbsp; 谢谢大家！(2004年2月2日)</div>

							
							
							
							
