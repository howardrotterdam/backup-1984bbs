<br />
 <br />
<br />
今天上午8时20分开始，支联会五名常委（梁国华、李耀基、李卓人、蔡耀昌和梁国雄）和古思尧分别被香港岛区总区重案组警员拘捕，指上述人士「于2009年12月25日，在中联办参与非法集结，作出扰乱秩序、威吓性、侮辱性或挑拨性的行为，相当可能导致任何人合理地害怕如此集结的人会破坏社会安宁，或害怕他们会藉以上的行为激使其它人破坏社会安宁。」五人将于3月18日（星期四）上午9时30分在东区裁法院第一庭出庭。<br />
<br />
 <br />
<br />
支联会强烈抗议警方的莫须有政治检控。支联会当日联同其它关注刘晓波的团体和人士，在中联办以和平方式抗议中共重判刘晓波11年，支持《零八宪章》。事隔两个多月，警方选择性地检控五名支联会常委，明显是针对和打压支联会，恐吓市民不敢参与支联会活动，以及声援内地维权及异见人士。这是彻头彻尾以言入罪，打压言论自由，侵犯《宪法》赋予的公民权利，践踏国际公认的人权。支联会将不畏强权，继续抗争，争取释放民运人士，平反八九民运，追究屠城责任，结束一党专政，建设民主中国。<br />
<br />
