（维权网信息员穆仔报道）3月11日上午8点左右，香港支联会五名常委梁国华、李耀基、李卓人、蔡耀昌、梁国雄和古思尧被香港总区重案组警员拘捕。指控他们于2009年12月25日，在中联办参与非法集会，有扰乱秩序及挑拨性的行为。他们五人将于本月18日上午9点30分在香港东区裁法院第一庭出庭。对此，香港支联会发表声明抗议。<br />
