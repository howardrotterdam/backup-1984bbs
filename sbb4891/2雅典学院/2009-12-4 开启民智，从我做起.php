25项公民守则，提高公共生命素质<br />
<br />
陈健民<br />
<br />
美国约翰霍金斯大学修养计划 (Johns Hopkins Civility Project) 创办人P.M. Forni在他的 Choosing Civility一书中提出了25项处世守则，修养和公民性的英语都是 civility，或者我们就称之为「公民修养」吧。此字的字根是拉丁文 civitas，代表城市。此字亦是 “文明” (civilization)的字根，可见早年西方社会已认为城市生活有开启民智和教化礼仪的功能，令人进入文明的殿堂。 Forni提出要注意25项公民修养如下:<br />
<br />
1. 关注 (pay attention) C 留意他人的处境。在公众地方大声谈手机便是没有关注其它人可能受到骚扰。<br />
<br />
2. 肯定他人的存在价值 (acknowledging others) C 插队不单浪费了排队的人的时间，而且是否定人们的存在，否定往往引发冲突。<br />
<br />
3. 向好处想 (think the best) C 先往人家的好处想，不单令自已的心灵保持纯真轻省，亦会影向对方更真诚与您相处。在基督教文化中，有教导说接待陌生人，无意中便接待了天使。我在美国迷路、坏车时经常s到人们积极协助。但中国人「防人之心不可无」是根深蒂固的，我们对热情的陌生人会有所顾忌，结果是向人家浇了一盘冷水。其实往好处想不是盲目不设防，只是世上如果好人居多，就不应先假设对方是坏人，结果是大家崩紧神经做人。<br />
<br />
4. 聆听 (listen) C 当我们打断人家的说话，抢着提出自已的论点时，不单显示我们不耐烦，而且反映我们有「自恋」倾向，要将水银灯移到自己头上。打断人家说话的一种不显眼做法，是对人家说话的内容不作响应，然后自说自话。同事告诉你刚去过云南，你却未及追问她对云南的感觉，便兴高彩烈谈你两年前在云南的游历和您对云南的观感。您的目光注视在自己的过去，而不是当下的友人。我们要学习安静，让人家有机会说话。关掉电视、手机，不要想着下一个约会的事情，让自己聆听别人的观点和感受，也让自己逃离自我中心的世界。<br />
<br />
5. 兼容接纳 (be inclusive) C 每个人都乐于被人接纳。群体带给人安全感，甚至生活意义和方向，谁也不想被群体排斥。我们要特别小心不要孤立群体中的小数人，譬如一群人聚会时最好说普通话，不要用方言交谈而令其它人在旁边发呆; 留意找一些共同话题让人人参与，而非只有小圈子有发言权; 如果有新朋友加入聚会，应该总结一下之前的讨论，让他易于加入讨论；主持会议的人，应尽可能引导所有与会者发言，提高每个人的参与感。<br />
<br />
6. 言调温和 (speak kindly) C 深思熟虑、言调温和是公民修养的核心。我们不要低估语言的力量，它可以建立也可以摧毁听者的自我。善良的说话令身边的人生活在喜乐之中。有时，要懂得在陈述自己的观点后，让对方有说话的机会。要控制声量，大声说话令人受惊，窒息对话。争论时要以事论事，不要攻击对方的种族、国藉、性别或其它与论题无关的私人生活。 “你们女人就是这样不讲逻辑…”、“你们北方人就是那样不设实际…”都是不能接受。即使意见分歧，亦要把对方视为有血有肉的人，而非只是一个要被彻底打倒的谬误。想一想对方为何会接受这样(你认为是错误)的理念，有助于平心静气、化解分歧。必须尊重对方，不要把人的弱点作为取笑的对象、不要扁低人家的成就。用语言践踏人家是想抬高自己，是嫉妒，是自卑感作祟。<br />
<br />
7. 不在背后说闲话 (don’t speak ill) C 如果您经常以中性或正面的方式谈论他人，人们便放心坦诚地与您交往而不用担心您会在背后蜚短流长。您控制您的舌头，换来更多真诚的关系。<br />
<br />
8. 接纳和给予赞赏 (accept and give praise) C 不要吝啬赞美，它令对方心情愉快，亦令自己有正面的人生观。但{美必须出于真诚，否则变成花言巧语。要注意在工作地方不能随便赞美同事的外形，目不转睛盯着女同事亦会惹来反感。<br />
<br />
9. 尊重他人意愿 (respect even a subtle “no”) C 无论您的提议是出自最良好的愿望，人家说 “不”，就不应强求。有时人家为了保护我们的自尊，用种种间接的方式向我们说 “不”，我们应该领情，而非因为人家未有明言，便穷追不舍。为甚么我们不愿意面对人家的拒绝? 因为我们还像小孩一样自我中心，希望世界按着我们的喜好运转。有时是因为我们自信心太低，觉得人家拒绝我的好意，便是把我全盘否定。<br />
<br />
10. 尊重他人意见 (respect others’ opinions) C 是否真能做到这一点，便要看我们采取怎样的方式与人争论。有几个要点应该注意: i) 即使您只是有保留地接受对方的观点，亦不应全面否定他人的主要论点:「我同意一般来说您的观点是对的，但在一些情况下….」ii) 即使您不同意对方观点，亦无需认为对方是全不合理的: 「您的想法的确有其说服力，但是….」iii) 容许自己有改变看法的空间: 「我现在还未被您说服，或者我对这问题还是了解不够。」iv) 明白有些事情是见仁见智: 「您说得没错，但如果换一个角度看这事情….」。讨论问题时最重要的是把自己的意见看为一种观点，而非绝对的真理。容许有异议的空间，甚至应主动征求参与者发表意见。<br />
<br />
11. 身体护理 (mind your body) C 好好打理自己的身体是对自己和他人的尊重。干净而没有异味的身体和头发、指甲不藏污纳垢、干净的牙齿和清新的口气，令您在公共生活中，无论在公交车、地铁、或者与人讨论时更有自信，亦令人更愿意与您交往。我们亦要注意身体发出的声音: 打喷嚏、打呵欠、倒胃气、放屁都会令人感到不适，应该用手帕遮掩或者躲入洗手间处理。<br />
<br />
12. 迁就他人 (be agreeable)<br />
<br />
13. 表达自己 (assert yourself) C 过度迁就他人而失却自我对精神健康有害，适当时候要表达自己的意愿和意见，拒绝过度的要求。<br />
<br />
14. 保持安静 (keep it down and rediscover silence) C 燥音带来精神紧张、高血压、失聪，我们有责任留意自己制造的声浪 (如谈话、音乐)是否骚扰他人。<br />
<br />
15. 尊重他人的时间 (respect other people’s time) C 守时是对他人时间的尊重，迟到便应设法通知对方，到达后必须向各人道歉。<br />
<br />
16. 尊重他人的空间 (respect other people’s space) C 不要挤拥他人、让人们先离开一个空间 (升降机、车厢)才进去。<br />
<br />
17. 真诚道歉 (apologize earnestly)<br />
<br />
18. 避开私人问题 (avoid personal questions) C 除非彼此已进入朋友关系，在公共生活中避免询问人家的年龄、婚姻状况、有否异性好友、为何不生孩子、为何看医生、做了甚么手术、收入和消费情况等。对某些国家的人来说，详细询问他人的政治取向(如投票记录)，或者宗教信仰(如是否经常上教堂和祈祷)，可能视为侵私隐。<br />
<br />
19. 好好接侍访客 (care your guests)<br />
<br />
20. 做一个为他人设想的访客 (be a considerate guest)<br />
<br />
21. 不要随便要人行方便 (think twice before asking for favors) C 特别是当那件事会为第三者带来不便或者违反纪律，我们便陷他人于不义。<br />
<br />
22. 不要只懂批评 (refrain from idle complaints) C 与其咀咒黑暗，不如点亮蜡烛。人生悲喜交集，每天有得有失，在乎我们选择以何种态度观之。不断投诉叫自己亦叫身边的人疲累。<br />
<br />
23. 接纳和提出建设性的批评 (accept and give constructive criticism) C 批评是严肃的事情，批评之前应确定自己是想解决问题而非藉此侮辱、操控、或报复对方。弄清楚问题所在、能否自控情绪、是否适当的时空作出批评等。忠言逆耳，批评令我们直面一些自己无法或不愿意面对的问题。但如果批评是无理取闹，便要学习如何断然否认，却又不会流于互相谩骂。<br />
<br />
24. 爱护环境和仁慈对待动物 (respect the environment and be gentle to animals)<br />
<br />
25. 不要推御责任，怪罪他人 (don’t shift responsibility and blame)<br />
<br />
上述一些守则显然有其产生的文化背景，中国人对一些行为 (如人与人距离、交谈的声量)的阐释可能和美国人不同。最重要的是掌握这些礼貌、风度、谦让等守规最终是要表达一种 “觉醒” (awareness)，是对他人的存在和需要的一种关切。有了这种关切，自然懂得用文明的方式去过公共生活。<br />
<br />
