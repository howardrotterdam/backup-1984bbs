「白毛女应嫁给地主黄世仁，手里有了钱再奋斗」中共样板洗脑失败　新一代膜拜权钱 <br />
<br />
中共建政 60年，为保江山不变色，炮制了许多的革命样板人物，对民众进行教育和洗脑，希望人民对党和国家无私奉献。但这些人物备受质疑，中共倡导的价值观开始受到颠覆。武汉有大学生公开否定白毛女，认为她拒嫁给逼死她父亲的地主黄世仁是错误之选。内地学者指，中共享样板人物对新一代洗脑的做法彻底失败。权钱膜拜已成为新一代的主流。<br />
<br />
「现在大学生找工作这么难，嫁给『黄世仁』，等手里有了钱，可以再自我奋斗，实现自己的理想，这不失为一种好的选择。当年的白毛女也可以这样规划自己的人生，可她偏要和黄世仁对着干！」有武汉华中师范大学汉口分校女学生，前日参加内地文艺评论家、资深编辑熊元义与学生探讨流行文化的活动，其间对熊元义讲述白毛女的故事，她们这群 90年代出生的大学生不齿白毛女的行为，认为她太笨了，并提出种种质疑，引起一阵骚动。<br />
<br />
只要有钱 「年纪大也不要紧」&nbsp;&nbsp;<br />
 <br />
追逐名牌成为内地新一代目标。法新社<br />
<br />
《白毛女》是讲述剧中女主角喜儿（后人惯称白毛女）与地主黄世仁的故事，是中共享来教育民众「不忘阶级仇恨，牢记共产党恩情」典型样板戏。白毛女作为穷苦人家的女儿，更被奉为是无产阶级反抗剥削阶级的代表人物，她的故事还被列入学校教材，在内地老少皆知。在当日的研讨会上，一位女大学生小谢称，如果黄世仁生活在现代，可能是个外表潇洒、风雅的男士，加上有钱，「为甚么不能嫁给他呢？年纪大一点也不要紧。」文学院一名姓蔡的大一女生更称：「如果我嫁给黄世仁，可以拿他的钱捐给慈善事业，帮助有需要的人。」 <br />
<br />
样板人物缺历史真实　违背人性&nbsp;&nbsp;<br />
 <br />
剧中白毛女加入共产党的部队。<br />
<br />
作为中共体制内学者的熊元义对此深感震撼，料不到女大学生对白毛女的评价已演变成对权钱的膜拜。他只好称：「如果白毛女嫁给黄世仁，她会瞬间异化到另一阶层。」华中师范大学教授许祖华则认为，女生赞同白毛女嫁黄世仁「是对价值观的颠覆，不可取」，并认为「生命的价值尊严，要靠自己创造」。内地民间学者温克坚对本报表示，包括白毛女在内，中共原来塑造的很多样板人物，因为缺乏历史真实，同时违背人性，是典型意识形态的产品，因此备受质疑、嘲讽，甚至否定，都是很正常的，「这也说明，中共过去几十年对人民的洗脑，已经失败了，人们有了新的价值观。」除白毛女外，备受质疑的还包括中共在十．一期间大肆宣传的解放军英雄人物董存瑞，他在国共抗战期间舍身炸国民党碉堡，被内地媒体指造假；志愿军黄继光在抗美援朝战争中，用身体堵美军枪眼，被指「没有任何军事价值」。此外，一位曾获中共已故领导人毛泽东亲笔题字「生的伟大，死的光荣」的女英雄刘胡兰，更被北京大学副教授阿亿踢爆，经她对当年事件的亲历者采访所得：刘胡兰非死在国民党手上，而是自己的乡亲手中，「完全没有毛泽东赞颂那么伟大。」 14岁的山西少女刘胡兰因帮共产党，事后被中共包装成在国民党铡刀前慷慨就义的英雄。《长江日报》/本报记者 <br />
<br />
故事介绍村女拒入豪门做妾　逃上山做野人&nbsp;&nbsp;<br />
 <br />
剧中喜儿（即白毛女）的男友拟向地主黄世仁报仇雪恨。<br />
<br />
《白毛女》是上世纪 40年代华北农村流行的传奇故事。贫农杨白劳早年丧妻，与女喜儿相依为命。村中地主黄世仁垂涎她的貌美，欲霸占为妾，但被拒，于是设局向杨白劳放高利贷，然后趁除夕夜逼债，但杨白劳还不起，被迫在女儿卖身契上画押。除夕夜，杨白劳回家见女儿，痛不欲生，仰毒自尽。喜儿被黄世仁抢入宅，她不堪受折磨逃走，入深山做野人，直至中共共产党军队来到，在山洞中找到她时，已头发全白，俨如白毛女，共产党令她重见天日。该故事于 40年代被中共编成歌舞，教育民众不忘对地主剥削阶级的仇恨，不忘共产党救命之恩。中共建政后，又将它编成芭蕾舞、拍成电影，成为红极一时的样板戏，更成为中共为下一代进行洗脑、灌输阶级思想的重要文艺作品之一。 <br />
