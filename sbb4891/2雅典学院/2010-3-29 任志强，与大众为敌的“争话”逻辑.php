													<h2>任志强，与大众为敌的“争话”逻辑</h2>
						
						
																											<div id="postmessage_538115" class="t_msgfont">2009年10月15日，任志强在第五届中国地产金融年会上发言。他的脸上总是紧张的肌肉，夸张有余的表情，似乎向外传递着他看透了这个时代的“知识的骄傲和骄傲的知识”。<br />
<br />
“不得不承认，这个社会里的‘乌合之众’是一个常态，对乌合之众的迎合，是人们首选的表达方式。但很遗憾，我不迎合乌合之众。”－－任志强<br />
<br />
像任志强怀疑大众的每一句谩骂一样，人们也有权利怀疑任志强的每一次耸人听闻的言辞。也许正是在这样的吵闹中，我们的世界才会变得越来越具有多样性，我们才会远离遮蔽，远离盲从，渐渐成为一个个有权利、有思考的人。<br />
<br />
文_苏小和<br />
<br />
众人都说任志强又名“任大炮”，言下之意，是指任志强这个人喜欢信口开河，故作惊人之语。事实上，任志强近几年的确靠着他那张大嘴，赢得一身骂名，以至于有的人咬牙切齿，死活把任志强当成了中国房地产资本家的代言人。<br />
<br />
在这个仇富心态暴涨，房价把老百姓压得喘不过气来的年代，任志强当然是一个送上来的大好靶子。他夜以继日显摆自以为正确的观点，人们废寝忘食发泄自以为正确的愤怒。这个国家因为任志强，成了一台戏。<br />
<br />
从盲从到不迎合<br />
<br />
说实话，人们的愤怒事实上抬举了任志强。在一大串牛气冲天的房地产公司中，华远是个小得不能再小的企业。从规模上看，华远地产进入不了房地产企业的前50名；从产权上看，华远地产至今仍是一家由北京市西城区主管的国有企业。<br />
<br />
任志强左右不了中国楼市的大局，甚至连搅浑北京楼市的能力也没有；而国企的制度安排，表明任志强的个人财富，主要还是靠工资和奖金，尽管他在自己的子公司里可能做了一些产权改革的尝试，但华远地产国有体制不变，下面再怎么修改，也不过是一种变相的“联产承包”。也就是说，跟潘石屹、黄如论、杨国强、朱孟依这些地产大佬相比，任志强撑死了也就算一种“小富即安”，他不是那种富可敌国的商人，按当下的体制来看，任志强最多也就是一个比较富裕的、北京市面上爱侃大山的“小处长”。<br />
<br />
北京的大街小巷充斥着这种膀粗腰圆、唾沫星子横飞的“侃爷”，任志强对自己的“侃爷”风格并不忌讳，并且认为自己天生就关注政治，关注经济，喜欢宏大叙事：“我们这代人天生就是从政治的染缸当中染出来的。从小的时候就开始关心国际大事，关心政治，比如从抗美援朝开始到打倒四人帮等，这代人就是这样成长起来的，让我们不关心政治不太现实。”<br />
<br />
但任志强的不同之处在于，他认为自己的每一次侃大山，都是一次学术演绎。记得去年的这个时候，我和任志强长谈，他上来就言明自己的法学方法论：“律师在法庭上辩论，他肯定攻击对方认为最不应该被攻击的地方，他会选择攻击点，这是一种法学习惯。学过法律的人可能更多会从这些角度，或者从这些方法来思考。”<br />
<br />
在他看来，像马列主义的很多东西，比如“否定之否定”，实际上带有哲学和法学的方法论。这样的东西融合在一起，会让他从另外一个角度，或者从相反的方向看问题。“不但可以看到别人看不到的问题，而且可能提出一些别人不能理解的观点，这可能是我总是引起大众误会的一个原因。”<br />
<br />
很明显，任志强带有一种法学方法论的自信。当他对那些愤怒的人们说话，事实上他先入为主地给自己预设了一种法学启蒙的色彩。1980年代中期，任志强曾经以“贪污罪”被组织收监，在那一年多时间里，他只能看到法律方面的书，监狱不提供其他的书。因为要自我辩护，任志强不仅将《法学概论》这样的理论书读得烂熟，而且把一些相关的法律条款从头到尾背了下来。日后任志强笑谈道，是看守所的生活把他逼上了法学之路，从此，这种情绪激动、满嘴跑风，时不时惹出麻烦的人，竟然建构起了自己的法学思考习惯。<br />
<br />
面对铺天盖地的谩骂，应该说任志强是有所准备的，他会通过读书为自己找自信。我上次走进他的办公室，桌子上就放着《乌合之众》和《身份的焦虑》两本书。话语之间，他甚至翻开书，大声念阿兰・德波顿的话：“报纸会使情况变得更加糟糕。势利者通常并无独立的判断能力，他们无非是捡拾那些所谓的社会名流的牙慧。因此，势利者的观点和立场在极大的程度上受报纸导向的影响。”<br />
<br />
这里，任志强理解的“势利者”应该是指市井层面缺乏独立思考能力的大众，所以他自我解嘲地说：“不得不承认，这个社会里的‘乌合之众’是一个常态，对乌合之众的迎合，是人们首选的表达方式。但很遗憾，我不迎合乌合之众。”<br />
<br />
熟悉任志强的人都知道他是一个爱琢磨的人，这意味着，虽然媒体想方设法试图抓住他的某句话进行炒作，但他对媒体的怀疑与审视却一如既往。很多时候，任志强并不是一个迎合媒体的人，有的时候他甚至有些刻薄。所以，接触过任志强的媒体人都说他不好对付，很多人会在他毫不客气的质问下落荒而逃。<br />
<br />
圈子里有这么一个传说，一次，他对一个记者直接发飙：你什么都不懂， 也敢来采访我？我相信这样的传言不虚，事实上，我第一次走进任志强的办公室，他几乎没有抬头，继续改他的文件，说就一边改文件一边采访吧。后来，我们熟悉了，他才哈哈大笑，说他对媒体的态度乃是不得已而为之，他天生就是一个把话拧着说的人，凡事要反过来想想，不怀疑一下，浑身都不舒服。<br />
<br />
我能想象任志强一些时候对媒体的某种不屑，他的脸上那些总是紧张的肌肉，那些夸张有余的表情，事实上是他刻意流露出来的、自以为看透了这个时代的“知识的骄傲和骄傲的知识”。我曾经问他，一个戴过红袖章、当过红卫兵的中国人，难道他的血液里没有残留着那些打倒一切、否定一切的时代癫痫病症吗？他立即回答说，有，但他强调，正因此让他学会了思考。<br />
<br />
“那时候我很盲从，比如认为共产主义要来了、林彪就是接班人等等。盲从之后，会思考的人会发现，不能盲从，所以我才开始倒过来说话。”<br />
<br />
这是一个有方法论价值的自我解读。如果说以前大众盲从的是领袖，那么领袖走下圣坛之后，每个人的主要工作，就是不盲从大众。当我把这样的理解说给任志强，他刻板的脸终于有所展开，不再像过去那样，只是在媒体里强调某一个局部的观点，以至于受众仅仅感知他的片言只语，就开始破口大骂；更没有在愤怒之余，起身，中途离场，让那些兴趣高涨的人们忽然找不到对手。我们眼前的任志强，他的思绪回到了他自己，他自己的人生过程，他的知识结构，他的价值追求，他不时开怀大笑，仿佛所有的喜怒哀乐，所有的思考和追问尽收眼底。<br />
<br />
思考者的尴尬<br />
<br />
其实，采访任志强之前，我有一个预置性的对话姿态：绕过当下热闹的市场沸点，去做一次安静的、略带思辨色彩的对话。理由无他，乃是为了将任志强在一片谩骂之中加以还原。<br />
<br />
一个奇怪的现象便是：他可能一直在试图基于某种体系说话，试图说出一些常识，这样的常识看起来是单独的，孤立的，个别的，但稍微耐心去倾听，就能发现任志强的整体意义，不过，受众显然没有耐心，在几个孤立的词句之后，排山倒海的语言围攻便接踵而来。由此，一直试图思考的任志强忽然成为了某种闹剧中的靶子，而那些展开攻击的人们，事实上他们手中的武器更加苍白，空有一堆乌有的情绪而已。<br />
<br />
在他的身上，堆积着太多的疑问。那些惹来围攻的言说，是他信口开河嘛？在目前曝光率很高的一批中国企业家中，陈东升、毛振华、任志强可能是最具数据意识和文献意识的几个人，前两人乃经济学科班出身，师从董辅，而任志强唯一的大学经历，是在中国人民大学上了一个研究生班，他坦承没有进过几次课堂，不是一个好学生。如果听过任志强的演讲，应该能发现他每一次演讲几乎都连带着大量的数据和文献，这样来看，围绕他的诸多争吵几乎就是一个幽默，任志强自以为是地言说，听众简单快意地谩骂，某种由大多数人组成的词语暴力似乎形成了对任志强的包围。<br />
<br />
那他是不是既得利益阶层的代言人？任志强至今不过是一家国有企业的领导人，但他呈现的所有言说却似乎与自由市场经济有关。我相信大多数攻击任志强的人，都有一种公平的追求，都有某种仇富的心理。从一般意义上来说，屁股决定脑袋，任志强应该是一名国有经济的捍卫者，而不是市场经济的阐释家。但人们对任志强的攻击存在先天性的错位，似乎选错了目标。因为任志强显然不是一个“奸商”，不是一名“暴发户”。以制度论，他可能只是一名政府工作人员，不是一个完全意义上的商人。<br />
<br />
但吊诡的是，既然只是一个国企代言人，为什么在任志强的身上，又看不到通常意义上的遮遮掩掩，看不到那种有些虚伪的王顾左右而言他，很难听到那种官话和套话？<br />
<br />
有一种说法：王石这样的人因为无法改变万科的国有体制，只能靠爬山和NGO来实现自己的人生价值。如果这样的解释成立的话，那么任志强应该就是意识到自己这辈子成不了大企业家，所以他选择用思考，甚至是用学术思考来寻找人生价值。<br />
<br />
如果任志强真的是以学术思辨为生，那么，那些粗糙的争吵就有价值。面对任志强，我的问题在于，一个经历过饥饿、“文革”和开放的中国人，他为什么想成为一个有独立思考能力和行动能力的人，他今天的思考和行动，是否有些矫枉过正，是否又陷入另外一种简单的二元对立中，为什么在一片咒骂声中，他竟然可以如入无人之境，一个人自以为是，独自前行？<br />
<br />
一个丰富的人生案例，显然比那些莫衷一是的观点之争和利益之争更让人有兴趣。像任志强怀疑大众的每一句谩骂一样，人们也有权利怀疑任志强的每一次耸人听闻的言辞。也许正是在这样的吵闹之中，我们的世界才会变得越来越具有多样性，我们才会真的远离遮蔽，远离盲从，渐渐成为一个个有权利、有思考的人。<br />
