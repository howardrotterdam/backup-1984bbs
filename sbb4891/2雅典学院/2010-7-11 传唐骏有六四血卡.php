1992年拆拿国学生保护法案（Chinese Student Protection Act of 1992，简称“CSPA”）是一项由时任美国联邦众议员的加利福尼亚州籍民主党人南希・佩洛西提出的、旨在给予所有在1990年4月11日之前抵达美国的拆拿人民共和国国民永久居留权的法案。这项法案的通过还使得时任美国总统乔治・H・W・布什于1990年签署的第12711号总统令（Executive Order 12711）中规定的“暂时禁止将拆拿国国民驱逐出境”成为永久性条款。<br />
