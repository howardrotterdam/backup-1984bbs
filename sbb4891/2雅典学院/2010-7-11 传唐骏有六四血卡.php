唐骏博士无心世界杯决赛<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;作者：周卫强<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;唐骏博士毕竟是博士，虽然是花了一些银子，不过唐博士身家过亿，并不会<br />
心痛。相比当下的专升本，在职读博，唐博士其实没有更坏到那里去------那些<br />
干部的读书费用，都是别人支付的哩！<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;唐博士最近就在补交他的博士论文，他研究了一下世界杯比赛，得出结论说：<br />
“几乎所有强队在先失一球后就不是强队了，这就是心理素质问题”。非常有道<br />
理，而且可以直接应用于唐博士本人，你看他在被揭露学历有假之前，那是春风<br />
得意，信心满满，到处挣钱，出书，演说，教不谙世事的年轻人如何挣钱，如何<br />
成功；可是在造假面市后，忽然就象被“进了一个球”一样，让他方寸大乱，<br />
“这一事件确实让我最近比较烦”，而且“我的律师团队已开始相关的进程，准<br />
备通过法律途径应对方舟子”。可以看出我们的唐骏博士的心理素质了，也可以<br />
看出唐博士的这篇博士论文是符合了“西太平洋大学”的要求了。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;学历重要不重要，造假算不算严重的事情，这是随后而来的讨论，也总会是<br />
两批人马各说各的。学历当然重要，唐博士的粉丝去为他们的偶像辩护，然而他<br />
们的偶像自己对学历的在乎就出卖了他的粉丝们：要是不重要，花钱买学历干什<br />
么，虽然他不差钱；造假当然严重，唐博士那么一个伟大的人物，如果事情不严<br />
重，他如何会“最近有点烦呢”？<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;话要说回来，造假严重与否这个问题本身并不重要，更关键的问题在于是否<br />
会被发现，要估算一下被发现的概率。唐博士他自己其实也知道造假曝光会是严<br />
重的事情，但他造假就是在赌不会被发现。在中国，造假被发现的概率是微乎其<br />
微的，尤其是这种什么文凭之类不关他人的假，更不会有人去在乎。中国向来就<br />
是各家自扫门前雪的，连那些害死过人命的造假都还有人不管不顾。而在美国，<br />
被发现的概率就要大得多了。如果比尔盖茨在中国，可能也会弄一张xx大学的<br />
MBA；而唐博士在美国的话，恐怕也会无心于此。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;那些粉丝有说唐博士用假学历欺骗微软盛大只能怪那些公司的某些部门没眼<br />
光，这样的观点也流行于中国社会的各个角落。比方一个老实人被一个聪明人骗<br />
去一笔钱，旁观者会嘲笑那个老实人太笨，而同时当然也就是在赞美那个聪明人<br />
发财有方的了。虽然这些粉丝们有欺负弱者，幸灾乐祸的嫌疑，不过也确实道出<br />
了许多人发财或成功的秘密！<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;找出根本原因，是谁都愿意去做的一件事，很多人都提出过很多不同的理由。<br />
比如有人归结于法律的缺失，就会有一部新法问世，就像08年三聚氰胺事件后，<br />
编出一部食品安全法来；也有人认为不是法律不全，而是执行不到位，监管不力，<br />
听者有意，就会成立专门的办公室，或什么领导小组，比如为达到学术不端0容<br />
忍目的而成立的xx委员会，但似乎委员们都很空。这次在网上就又有人在提是什<br />
么什么缺失了，不知道这次是要编法律呢还是要成立委员会。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;原因，其实是异常简单的，不是人的问题就是制度的问题，这里一定要不去<br />
涉及根本制度，只指是法律制度规章制度。制度没有问题，那一定就是人的问题。<br />
按照现在的情况看，我们的法律制度规章制度已经是相当多而全且杂，让那些想<br />
考律师的人都感到变化太快了。所以恐怕问题不大，所以一定是人的问题。没人<br />
管嘛！可为什么没人管呢，大概因为是一路货色，物伤其类吧。可是中国人为什<br />
么喜欢这一套，倒是难以搞清楚，或许可能是智商的问题。即便我们的唐博士，<br />
如此成功的人物，智商其实也不过如此，他如果选择道歉，形象会不会因此而更<br />
光辉一些呢。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;唐博士当然不会道歉。撒谎是中国人的习惯，从古至今的历史书，没有一部<br />
是全说真话的，说真话会让我们感到别扭，而且，说真话也常常会付出惨痛的代<br />
价，比如有时候会被万岁爷砍头，有时候会被主席迫害，有时候就是恐吓的电话<br />
或夜路的闷棍，因此撒谎也算是中国人求全的一种手段，发展到现在，就变成<br />
“成功”的一种手段了。而且撒谎还产生惯性，谎言揭穿的第一反应就是辩解，<br />
用更多的谎言来充实第一个谎言。唐博士是很道地的中国人，一定也脱不了这个<br />
窠臼。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;然而中国如果真的要受世界尊敬，却必须要跳出这个窠臼，慢慢开始学习说<br />
真话，光有钱是远远不够的，即使那钱也是很白的那种。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;世界杯决赛还没有进行，但唐博士其实更适合于参与中国足球，他们比较的<br />
相像！<br />
