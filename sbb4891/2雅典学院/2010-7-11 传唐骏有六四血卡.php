<a href="http://www.cdjp.org/01q/archives/00000017.shtml" target="_blank">http://www.cdjp.org/01q/archives/00000017.shtml</a> </blockquote></div>【社区】能请谈一谈“六四血卡”和“政治难民”问题吗？<br />
（回复）“六四血卡”是对“六四绿卡”的贬称，“六四绿卡”指的是1990年美国前总统布什发布的关于保护中国学生学者的行政命令，这个行政命令规定凡是中国大陆出生，1990年4月1日之前在美国的中国大陆人士（不限于学生学者），都可以在美国合法居住而不需要其他签证，并且获得工作许可。大约在1994年，这批受到该行政命令优惠的中国大陆人士，绝大部分都通过简单的手续获得了美国绿卡。<br />
“六四绿卡”之所以后来被称作“六四血卡”，是因为布什发布的行政命令主要的保护对象是1989年6.4期间在美国中国大陆人士，而这项行政命令是以1989年6.4期间北京的学生和市民倒在中共军队枪口和坦克之下为基础的。而之后逃过了当时中共军队枪口和坦克的学生来到美国，却得不到这种保护和优惠。举例来说，一个1988年靠打小报告积极入党从而取得公派到美国留学的人，后来取得了“六四绿卡”，而一个1989年积极参加学生民主运动，在解放军的枪口和坦克下逃生的大学生，1990年5月份离开中国抵达美国，就不能取得“六四绿卡”。后者对美国的这项政策感到很不公平，“六四绿卡”问题在后来的中国大陆留学生中间，“美国并不公正、并不公平”、“美国并不真正支持中国的民主”、“美国只是为了掠夺人才”等阴影，直到今天也没有散去。<br />
<br />
虽然在1989年，当时代表在美国的中国大陆学生学者的学自联是站在利益的立场游说布什政府下达了这项行政命令，但美国总统布什发布这项行政命令却与“掠夺人才”无关，否则这项行政命令就不会把大批农民出生的中国大陆偷渡客也包括在内了。我们相信，当时的布什政府，受到89学生运动规模的感动，也看到海外中国大陆学生、学者，非法和合法移民对中国民主运动支持和参与的热情。在1989年6.4期间，一个非法在唐人街衣厂打工的农村偷渡来的女工，会慷慨地向声称为北京学生募款的民运组织人士捐赠百元美钞，类似动人的情况被广泛报道过。我们认为，布什总统行政命令的政治目的，可能包含了在美国保留一股中国大陆人士的促进中国民主的势头和反抗中共专制独裁的力量，以使中国大陆早日实现民主自由的政治制度。<br />
<br />
但是，布什行政命令想要达到的政治目的不但没有能够达到，而且还造成了日后中国大陆留学生对美国政府的政策产生偏见和存疑。其实，我们在日后的十多年中发现，真正的中国大陆民主力量集中在1989年在中国大陆亲身参与过学生运动的当时的学生和城市青年当中，这些当时的学生和青年人有过不顾自己的利益，表现出为理想而献身的勇气，也冒过大大小小风险，他们还影响了周围的一大批人和后来的年轻人。他们中间的大多数对民主自由理想从来不曾消失，但是他们在行动上却放慢了脚步，有的甚至停止了脚步。“六四绿卡”的问题，让他们不知道他们本来认为一定是愿意支持和促进中国大陆民主化的美国政府为什么言行不一致。美国是全球民主自由的主要鼓吹者和领导者，他们原以为美国是中国民主运动力量的坚强后盾，可是“六四绿卡”问题却使他们对美国存疑和失去了信任，结果导致后到美国的一批批本来应该成为在美国的海外民运主要力量的群体，在来到了美国之后，因为对“不公平”的愤怒，不愿意加入民运行列，抵制民运甚至反对民运，因此没能形成一股促进中国民主的势头和反抗中共专制独裁的力量。<br />
<br />
“六四绿卡”所保护的当时的公费留学生、访问学者，包括其他一些自费留学生和访问人员在内，他们中相当大的部分是共产党政权的宠儿和既得利益者，他们还是在恶劣的政治环境下激烈地为利益竞争取得优势才公费留美深造，他们获得了“六四绿卡”又一个既得利益之后，下一个追求的利益目标就是将来回中国不要跟中国政府有麻烦，能够体面地回国安全地投资和购置产业，或“叶落归根”，等等。因此在这个群体中不可能保留和形成一股中国大陆人士的促进中国民主的势头和反抗中共专制独裁的力量。<br />
<br />
抛开海外民运中的纷争这些“小气候”，我们可以看到上述的“大气候”是造成中国大陆海外民主运动力量在6.4之后不久，特别是在1990年4月布什总统发布行政命令之后，开始走下坡，直到一度接近“死亡”的症结所在。我们的看法是，美国应该看到形成一股中国大陆人士的促进中国民主的势头和反抗中共专制独裁的力量的群体依然存在，他们就是1989年亲身经历了民主运动的当时的学生和青年，以及他们之后的年轻人。但这是美国政府的事情。<br />
<br />
中国的民主不能依赖美国，中国大陆有心为中国的民主事业奋斗的人士，不能因美国政府政策的失当而失去对自己追求理想的信心和停止行动的步伐，毕竟我们应该这样理解：我们中国大陆人士追求在中国大陆实现民主自由，是为了中华民族的永久和平、幸福和发达，这一切都是我们中国人自己的事情，而不是为美国政府而做的。我们不能继续让“六四绿卡”所造成的阴影继续阻碍我们海外民主运动力量的发展壮大，国内也不能让美国政府曾经失当的政策，继续成为对美国支持中国大陆实现民主自由政治体制的立场持怀疑和偏见的理由。说得过份一点，我们应该“给美国一次机会来纠正过去的错误”，但这必须是中国大陆的民主运动人士首先不要放弃和停止步伐才行。<br />
<br />
关于“政治难民”问题，这里作最简单的回答。举一个例子，古巴的“政治难民”，一直是在美国的海外反专制独裁的主要力量，每当有古巴政治难民抵达美国，他们都会举着美国国旗去欢迎新来的人，努力去帮助他们。把中国大陆的政治难民变成一股海外民主运动的主要力量是理所当然的事情，也是各主要民主国家接收专制国家的政治难民的政治利益（或者叫作这些国家的“公共利益”）所在。<br />
<br />
目前中国民主运动的问题，不是该不该把中国大陆的政治难民变成一股海外民主运动的主要力量的问题，也不是该怎么做的问题，而是海外民主运动的领导人们要把大家领向一个什么方向、去实现什么目标的问题。如果中国大陆海外民主运动的某些领导人执意要把大家领向一个替国民党向共产党复仇，或者替民进党实现台独这些方向的话，中国大陆的政治难民在美国再多，我们想象中的“民主力量”是不可能形成的，这也是中国大陆的海外民主运动为什么受到耽误的症结之一，虽然这个问题正在淡化，但到目前为止并没有消失，所以这方面的“民运内斗”还在继续。（回答者：沈龙、石磊）</div>

							
							
							
							
