　　<br />
　　 <br />
　　<br />
　　1， 根据美国1933年 Securities Act；上市公司老总对于移交证监会审核的公开披露信息负有全责；而实质性误导信息是导致股民对上市公司对集团诉讼的一个诉因 （Cause of action)。对高管信息造假就符合这个实质性信息误导(Materiality)的要求，《香港证券法》对此有类似的规定。因此，唐骏的学历和工作经历如果有造假行为，那么美国和香港的上市公司的股民可以对上市公司提出诉讼，要求赔偿。这种诉讼一旦有了第一个股民起诉，很可能引起集团诉讼。<br />
　　<br />
　　 <br />
　　<br />
　　2， 美国对律师和医生管理非常严格。拥有律师资格，必须要在美国律师协会（American Bar Association）认可的法学院取得JD学位（某些州可能允许LLM学位）之后才有资格参与各个州的律师资格考试。 此外，所有律师还必须通过全美律师的一个考试（Multi-State Exam）。 在此之后才能参加与法律相关的工作。没有律师执照又从事于法律相关的工作本身就是违法行为，根据美国加州的法律，这种非法从事法律工作（ unauthorized practice of law）最高可能得到刑事处罚。特别是出庭资格，法官一定会严格审查他的律师资格。如果唐骏先生真的在没有执照的情况下非法出庭，唐先生回到加州有可能面临起诉。<br />
　　<br />
　　 <br />
　　<br />
　　3， 归化入籍的有关规定（Neutralized citizenship）：根据美国《移民法》规定，规划入籍的公民，如果在申请归化前有违法行为（包括隐瞒违法事实），其归化移民资格会被取消。而留学生获得F1签证或者是交流学者获得的J1签证是不允许工作的。 可以开公司，但是不允许打工。 唐骏先生应该担忧的是他声称的“这个公司”是否有收入。 这种收入是工资还是利润。 如果是工资要考虑是否交税。如果没有纳税纪录，那么就违反了美国税法，根据其逃税的程度，可能是重罪。如果有纳税纪录，那么这个纪录可能成为违法打工的铁证，也就违反了美国《移民法》，属于非移民签证身份在美国非法打工，由此能影响他的身份，一旦查证，唐骏先生可能面临取消身份，下次回美国有可能面临递解出境（deportation）。此外，作为美国公民，唐骏在中国的收入必须交纳美国的个人收入所得税。按他1亿人民币的年薪，他大约要交纳4千万的税。如果未交美国税，那么有可能面临美国政府对他的起诉。<br />
　　<br />
　　 <br />
　　<br />
　　关于唐骏先生拟起诉方舟子先生：<br />
　　<br />
　　首先，根据实体法的规定。 诽谤 必须具备三个法律要素 第一， 讲的不是事实；第二， 有损于原告人身份尊严人格； 第三， 要有公共传播。中国和美国的法律都对此有类似要求。<br />
　　<br />
　　如果方舟子讲的是事实就不构成诽谤，唐骏在实体法上无法设胜诉。<br />
　　<br />
　　其次，根据程序法的规定，这种案件只能是在中国审理。 唐骏所说的在美国起诉只能说明他对美国法律一无所知。此事发生在中国，方舟子是中国人，美国法院对方舟子没有管辖权（Jurisdiction）， 即便是根据美国的长手臂法（Long Arm Statutes），即便唐骏真是美国公民，美国法院一般也不会受理此案，因为诉称的侵害行为发生在中国，侵害后果发生在中国。退一万步说，即便美国法院认定有管辖权，也会依据“地点不便”（forum or venue non convenient）的原则驳回唐骏的起诉. 方舟子大可不必担心要到美国来应诉。</div>

							
							
							
							
