																											<div id="postmessage_791845" class="t_msgfont">“性早熟”恐慌<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;・方舟子・<br />
<br />
&nbsp; &nbsp; 婴儿配方奶粉再一次让国人的神经绷紧。武汉三名消费者声称其宝宝吃了<br />
圣元奶粉后出现性早熟，经众多媒体热炒，终于惊动卫生部组织专家调查，公<br />
布检验结果称圣元奶粉中未检出外源性性激素，内源性雌激素则在正常范围，<br />
三名女婴属于临床常见的单纯性乳房早发育，与圣元奶粉无关。<br />
<br />
&nbsp; &nbsp; 卫生部的权威并没能平息事态。网上的调查结果表明大部分网民都不接受<br />
卫生部的结论，认为调查组的专家被圣元公司公关。在卫生部公布调查结果的<br />
前几天，我在微博上表示过与调查结果相似的看法，很多人也认为我“被收买”、<br />
“受招安”。他们坚定地相信，既然有人吃了圣元奶粉之后出现性早熟，就证<br />
明了圣元奶粉肯定有问题。<br />
<br />
&nbsp; &nbsp; 这些人缺乏统计概念。我们只需做一番简单的计算，就可以说明他们的信<br />
念是站不住脚的。武汉人口约900万，出生率约8‰，即每年大约有7万名新<br />
生儿诞生，其中一半是女婴。那么现在武汉0～2岁的女婴大约有7万人，其中<br />
大约有65％吃配方奶粉，圣元奶粉占的市场份额为10％，也就是说，武汉有大<br />
约4500名0～2岁女婴吃了圣元奶粉。单纯性乳房早发育的发病率是千分之二，<br />
武汉这些圣元女婴中会有9人碰巧出现单纯性乳房早发育，其中有3人被媒体<br />
“曝光”并不算多。反之，如果圣元奶粉的成分真的有问题，就不会只有这<br />
几个病例。吃其他品牌的奶粉的女婴当然也有性早熟的，只不过没有引起联想<br />
或关注而已。<br />
<br />
&nbsp; &nbsp; 性早熟有多种情形，原因复杂。女孩如果只有乳房发育而没有其他症状，<br />
叫做单纯性乳房早发育，具体原因不明。卫生部专家认为婴幼儿的单纯性乳房<br />
早发育多是“微小青春期”所致。这对几乎所有中国人（包括中国医生）来说<br />
都是一个新名词，在此之前没有中文文献有此说法，对中文医学论文的检索结<br />
果是零。于是被怀疑卫生部专家是为了替圣元奶粉辩护杜撰出来的术语，网上<br />
出现了一些对此揶揄讽刺的文章，说是“神奇国度的神奇专家的神奇发现，2010<br />
年诺贝尔医学奖非他莫属”。<br />
<br />
&nbsp; &nbsp; 这实在是高估了中国医学专家的水平了。实际上英文文献早有这种说法<br />
（写作mini-puberty或minipuberty），在一些儿科教科书、论文中都用过。<br />
国际期刊《儿科内分泌学综述》在2005年曾发文讨论这个术语是否恰当。无论<br />
如何，它描述的现象是存在的。性激素是性腺（卵巢和睾丸）分泌的，它<br />
受下丘脑和垂体分泌的激素的控制，“下丘脑－垂体－性腺”被称为生殖轴。<br />
在婴儿时期生殖轴已经相当成熟，出生后促卵泡激素（能刺激雌激素的分泌）<br />
在女孩中显著上升，在出生2～3个月时达到峰值，然到逐渐下降，直到12～18<br />
个月时降到青春期前儿童的范围，但是直到4岁时其含量仍比青春期前儿童的高。<br />
在这段时期有的女婴会出现乳房组织增大和阴道粘膜的变化。<br />
<br />
&nbsp; &nbsp; 单纯性乳房早发育是一种良性状态，其实算不上病，无需治疗就会自己消<br />
失，只需定期检查以排除潜在的其他病因。但是国内临床医生却往往把它当成<br />
疾病治疗，例如用“滋阴泻火”、“疏肝理脾”、“行气活血”的中药治疗，<br />
效果当然都非常好（本来不治也会好嘛），这些药物有什么副作用、会对女婴<br />
健康产生什么不良影响则是没人管的。<br />
<br />
&nbsp; &nbsp; 医生即使不开药，也会要求患者忌口，避免吃牛奶、鸡蛋、鸡肉之类的被<br />
认为含雌激素比较高的食物。传统上有“病从口入”的说法，一生病就想是不<br />
