本的检测结果有所差异，但其中雌激素浓度大约都是每毫升几十皮克。即使一<br />
天喝一升牛奶（一般人不可能喝这么多），即使牛奶中的雌激素全部被人体吸<br />
收（实际上不可能），也不过几十纳克。但是青春期之前的儿童身体自己制造<br />
的雌激素的量每天已达到大约10微克，是牛奶的数百倍。所以牛奶中的雌激素<br />
