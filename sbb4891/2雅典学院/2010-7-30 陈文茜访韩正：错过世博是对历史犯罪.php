韩正：我是希望城市低碳这个理念不能只是城市的管理者接受，我觉得是全民都一定要接受这个理念。所谓低碳，如果讲到城市运转，它实际上就是一个节能的概念，就是最低限度的耗能，它就可以做到低碳。衡量一座城市的低碳发展我觉得有两个指标是非常重要的，第一个指标就是你的经济生产总值中间的单位能耗，这个指标就反应你的生产力水平和科技水平。第二个指标是人均的能耗是多少，因为用煤，用电，它可以转换，转换成同一个单位，人均的能耗是多少，这也是很重要的一个衡量指标。所以，这两个指标应该能够很好地运用，这是未来城市必须要这么做的，我觉得上海就应该这么做。上海现在的能耗，从生产总值的单位能耗来讲，在全国是比较低的，但是我们和全世界先进水平比，还是比较高的。所以，这一方面我们还要进一步努力，要吸取世界上所有的好经验。这就涉及到一个产业结构调整，发展方式的转变。同时，那个人均的能耗指标，它反应的是两个重要方面，一个重要方面是反应你的发展水平和你的人民生活水平，因为原始社会没有太多能耗，发展中国家能耗也很低，如果你要享受很好的生活，肯定会带来很大的能耗，但是这只是一个方面。另一个重要方面是你达到同样的生活水平后的能耗是多少，其实反应的是人们节能的观念，反应你的技术水平，反应了你的节能技术在实际生活中的运用，这些指标都能体现。我觉得上海未来的发展应该更加注重这些指标。<br />
<br />
试运行是为了改进工作寻找问题<br />
<br />
陈文茜：这一次的上海世博，其实它已经成为大家公认的100年来、甚至是159年来最好的世博会，是盛大的世博会。可是因为一开始的时候试运行场面非常地混乱，甚至引起了香港媒体大篇幅关于打架等的报导。很多国际媒体说，人们说这场世博会不来会后悔，来了更后悔。当然最近这个世博会已经上轨道。有很多人说，你们不应该一开始的时候，把这个免费票，跟试营运的票，送给这些为世博会牺牲的人，因为造成世博会第一个给外界非常坏的印象。你现在回头来想，不知道市长怎么看？<br />
<br />
韩正：我不这么认为！首先世博会我们决定是要试运行的，试运行的目的是什么？我们希望通过试运行能够使5月1日开始的运行能够非常圆满，因此试运行的目的在于完善我们的工作，或者讲通过试运行来检查我们哪些方面工作还不足。因此我们把试运行看作一个改进工作、寻找问题、甚至是暴露一些问题的方法，这样有利于我们更有针对性地去改善工作，试运行就是这样一个目的。如果我们认为什么都好了，那就不用试运行了，5月1日开园就完了嘛。试运行我们想肯定会有一些我们没想到的问题出现。试运行当然不能卖票了，只能送票了。世博会人人有贡献，全上海的老百姓，全中国人民都有贡献，所有参展方都有贡献。那么我们希望能够对直接有贡献的人进行慰问，比如为了世博动迁的居民，还有建筑工人。4月20日到4月27日我们一共举办了六场试运行，全部是按照计划。试运行的第一天，我在现场，后几天我也在现场看，看了也是很着急。早晨本来想九点开门，我们世博会九点开园，我们试运行也九点开园。等不到九点了，已经人山人海很拥挤了，就提前开园。后来，我们检查下来，觉得不能怪参观者，首先我们要检查我们的组织者、管理者，我们的工作还有一些什么没做好。我现在可以跟你说，有很多工作是我们还没有做好。比如，为什么开始门口这么拥挤，我们第一天试运行20万人，800多个安检门我们没有全部开，只开了一部分。后来我们全开的，如果那天全开，门口不会有那么多人。再比如，垃圾有的时候掉在地上，我就发现我们在设计世博园垃圾箱的时候，设计得太小了，是装满了，装不了了，怎么办？装满了，再装进去肯定就掉出来。<br />
<br />
陈文茜：所以你不觉得那些媒体故意丑化，你觉得是你们垃圾箱做得不够好。<br />
<br />
韩正：我觉得是我们垃圾箱做的不够大，垃圾箱放的密度不够，当然我们对产生垃圾的数量估计也不够。这也是我们管理者的问题。再譬如，门口参观排队有的次序不是非常良好，我觉得我们也要从我们工作中来检查问题。后来你就发现为什么会那么好，因为一排一排的隔离的蛇行排队设施上去了，开始没有啊！没有隔离，地上又没有画线，你说要排得那么整齐，我看谁都做不到，让我们去排队，我们也不可能排得那么整齐。我们要理解参观者，我排了两个小时，突然有个别人会插队，他心情非常不舒服，他就会往前挤，就打架。往前挤了以后，打架是非常个别的，20万人里面可能打架就一两个人。如果有了这些措施以后，这些问题不会出现。<br />
<br />
希望世博会为上海留下两份财富<br />
<br />
陈文茜：你们所有的人觉得200多年来这是个机会，如果没有让它脱胎换骨，如果没有改变，你们是历史罪人。事实上我自己从世博会倒数600天、倒数300天、试运行看，我是非常敬佩上海这一次的改变。但是，我也看到有人说上海世博是用钱堆出来的。还有一些国际媒体对你们的报导。你觉得这是对中国的歧视、对上海的诋毁，还是？你自己怎么看待？<br />
<br />
韩正：任何事情都会有一些不同的一些想法、不同的意见，最后还是以事实来说明，事实是什么样子，就是什么样子。我也可以把目前的情况向您做个介绍。世博会在筹办过程中，我们向国务院报了，世博会筹办的经费两部分。第一部分世博园要建设，我们当时测算以后，大概180亿人民币建设经费，现在建设已经完了，我们没有突破180亿，具体什么数字，现在还在审计，要一笔一笔审，因为工程要决算，决算以后就审计，审完以后向全社会公布。第二部分就是5月1日以后的运行经费，我们原来估算大概是106亿人民币，从目前情况来看，可能运行经费会超过106亿，我估计超过不会很多，当然这个就是现在每时每刻都在跟踪的，也进行审计，到世博会一结束运行经费是怎么用的、到底多少钱也会向全社会公布。<br />
<br />
陈文茜：当然韩市长你现在都还在世博会举办的紧张状态，每天都有状况要发生。你每天都在不断的适应，不断的调整这样。但是我猜想有一天，你能够稍微空下来，当你再站在外白渡桥，看上海一切的改变，我想您会有很特殊的心情。在我们整个采访最后，要不要跟我们观众分享一下？<br />
<br />
<br />
韩正：上海在世博举办以后，我希望能够留下两部分的财富，一个是硬件的方面有形的财富，世博园就是有形的财富。另一个方面是无形的财富，精神方面的财富，这对于未来上海城市是很重要的。当然，你刚才讲的外白渡桥，那个外白渡桥是102岁了，当它100岁的时候，我们对它进行了彻底的大修，大修完是去年的5月份，也有一年了。外滩也进行了重新改造，外滩有多次改造，我想这一次的改造后应当在未来很多年里都不会再去进行改造。所以，外滩是进一步的来见证上海变迁、上海的历史，外滩也进一步地证明上海这座城市会越来越美好，生活在上海这座城市里的人民会越来越幸福。谢谢！<br />
<br />
(本文来源：东方网 )</div>

							
							
							
							
