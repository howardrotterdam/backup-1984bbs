													<h2>韩正:错过世博是对历史犯罪</h2>
						
						
																											<div id="postmessage_766161" class="t_msgfont"><a href="http://expo.163.com/10/0730/10/6CR6FK470094431K.html" target="_blank">http://expo.163.com/10/0730/10/6CR6FK470094431K.html</a><br />
<br />
中共上海市委副书记、市长韩正日前就抓住世博机遇推进科学发展等问题，接受了台湾著名主持人陈文茜的专访，专访节目在台湾和香港播出后引起良好的社会反响。<br />
<br />
时代给了上海一次机会<br />
<br />
陈文茜：黄浦江边的世博会，159年的等待，黄浦江千年的历史，数百年的污染，所有的改造，上海真正发达起来在中国历史里作为经济中心，大概差不多160年的光景，应该是从江南制造局开始。然后所有的改变，它最大的一场改变，就在你人生从48岁到56岁这段时间，您自己的感受是什么？<br />
<br />
韩正：个人的作用可能是微不足道的。上海的改变，最重要的是时代给了我们一次机会，全世界这样一种发展的潮流，时代的潮流，给了上海一次机会，中国的改革开放给了上海一个特殊的舞台。也就是说，如果没有中国的改革开放，肯定不会有上海的变化；没有中央决定上个世纪90年代初的浦东开发开放，也肯定不会有今天的上海。当然，我们遇到了好机会，遇到了好时代。作为城市的每一个管理者，每一个领导者不会错失这样的机会，如果任何一个领导错失了这样的机会，那是对历史的犯罪。所以上海的历届市委市政府，每一位领导成员都认识到了这一点。<br />
<br />
陈文茜：历史的犯罪？<br />
<br />
韩正：就是你不能错过这个机会，你错过这个机会就对不起上海、对不起子孙、对不起上海人民，更对不起国家。您刚才讲到的就是世博从申办成功，一直到举办这八年，这八年正好是我们国家改革开放30年的最好的时机之一，也是上海浦东开放开发以来发展得最好的时期之一。再过几年，上海的人口大概就是全世界最多的城市。现在我们称上海是全世界人口最多的城市之一，我想再过几年，这就不是之一了，就是人口最多的城市。人口最多的城市，我们作为政府的管理者，所有工作的基点是什么？你所做的一切工作的目的是什么？我想所有的一切归结到一点，就是让这个城市的人民生活更幸福，如果离开了这一个基点，所有的工作就无从谈起。<br />
<br />
陈文茜：我们也知道，你们在这一次除了申博的规划、世博的规划之外，也规划了2020年的国际金融中心。那你也预期到上海必将成为全世界人口最多的城市。届时这个城市可能面临交通、可能面临城市生活、可能面临它的整个环境的问题。<br />
<br />
韩正：我们有这么一个预测：现在上海常住人口在2000万多一点，我们估计可能会到2020年的时候，按照现在的发展趋势，上海的人口可能未来会以每年35万，甚至于更多的人口增长。过去五年大概平均每一年是35万。我估计未来可能是35万更多一点，所以按照这样一个发展趋势，到2020年的时候，上海肯定是全世界人口最多的城市。人们向往城市生活，因为城市给人们带来的幸福指数不断地提高，但是当人口集聚到一定程度，这给城市也带来很多问题，人们会感觉到与幸福指数不相关的城市的一些负面作用。比如交通问题，比如每个城市都遇到的一些城市贫困人口问题。你说每个大城市，城乡结合部，城市的边缘地区，在某一个区域它有贫困人口的问题，这些问题每一个城市都遇到。环境问题、交通问题，是世界所有城市发展过程中，城市的管理者都会遇到的难题。<br />
<br />
城市最佳实践区给人启发<br />
<br />
