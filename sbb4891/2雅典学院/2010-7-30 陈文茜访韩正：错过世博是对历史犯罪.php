													<h2>陈文茜访韩正：错过世博是对历史犯罪</h2>
						
						
																											<div id="postmessage_765668" class="t_msgfont"><a href="http://expo.163.com/10/0730/10/6CR6FK470094431K.html" target="_blank">http://expo.163.com/10/0730/10/6CR6FK470094431K.html</a><br />
<br />
中共上海市委副书记、市长韩正日前就抓住世博机遇推进科学发展等问题，接受了台湾著名主持人陈文茜的专访，专访节目在台湾和香港播出后引起良好的社会反响。<br />
<br />
时代给了上海一次机会<br />
<br />
陈文茜：黄浦江边的世博会，159年的等待，黄浦江千年的历史，数百年的污染，所有的改造，上海真正发达起来在中国历史里作为经济中心，大概差不多160年的光景，应该是从江南制造局开始。然后所有的改变，它最大的一场改变，就在你人生从48岁到56岁这段时间，您自己的感受是什么？<br />
<br />
韩正：个人的作用可能是微不足道的。上海的改变，最重要的是时代给了我们一次机会，全世界这样一种发展的潮流，时代的潮流，给了上海一次机会，中国的改革开放给了上海一个特殊的舞台。也就是说，如果没有中国的改革开放，肯定不会有上海的变化；没有中央决定上个世纪90年代初的浦东开发开放，也肯定不会有今天的上海。当然，我们遇到了好机会，遇到了好时代。作为城市的每一个管理者，每一个领导者不会错失这样的机会，如果任何一个领导错失了这样的机会，那是对历史的犯罪。所以上海的历届市委市政府，每一位领导成员都认识到了这一点。<br />
<br />
陈文茜：历史的犯罪？<br />
<br />
韩正：就是你不能错过这个机会，你错过这个机会就对不起上海、对不起子孙、对不起上海人民，更对不起国家。您刚才讲到的就是世博从申办成功，一直到举办这八年，这八年正好是我们国家改革开放30年的最好的时机之一，也是上海浦东开放开发以来发展得最好的时期之一。再过几年，上海的人口大概就是全世界最多的城市。现在我们称上海是全世界人口最多的城市之一，我想再过几年，这就不是之一了，就是人口最多的城市。人口最多的城市，我们作为政府的管理者，所有工作的基点是什么？你所做的一切工作的目的是什么？我想所有的一切归结到一点，就是让这个城市的人民生活更幸福，如果离开了这一个基点，所有的工作就无从谈起。<br />
<br />
陈文茜：我们也知道，你们在这一次除了申博的规划、世博的规划之外，也规划了2020年的国际金融中心。那你也预期到上海必将成为全世界人口最多的城市。届时这个城市可能面临交通、可能面临城市生活、可能面临它的整个环境的问题。<br />
<br />
韩正：我们有这么一个预测：现在上海常住人口在2000万多一点，我们估计可能会到2020年的时候，按照现在的发展趋势，上海的人口可能未来会以每年35万，甚至于更多的人口增长。过去五年大概平均每一年是35万。我估计未来可能是35万更多一点，所以按照这样一个发展趋势，到2020年的时候，上海肯定是全世界人口最多的城市。人们向往城市生活，因为城市给人们带来的幸福指数不断地提高，但是当人口集聚到一定程度，这给城市也带来很多问题，人们会感觉到与幸福指数不相关的城市的一些负面作用。比如交通问题，比如每个城市都遇到的一些城市贫困人口问题。你说每个大城市，城乡结合部，城市的边缘地区，在某一个区域它有贫困人口的问题，这些问题每一个城市都遇到。环境问题、交通问题，是世界所有城市发展过程中，城市的管理者都会遇到的难题。<br />
<br />
城市最佳实践区给人启发<br />
<br />
陈文茜：那如果以这一次的上海世博为例，你们最大的创意是城市最佳实践区，那里就真的让上海学到经验：什么样的房子、什么盖法、什么样的建材可以再生利用、可以保持恒温、可以让它看起来舒服一些。我自己也看了城市最佳实践区，也看了这一次世博会里头像荷兰馆、挪威馆，这两个馆呢，好象有一个馆是从黄浦江抽水给大家喝，挪威馆里头有一个大机房，就是告诉大家如何把很脏的水透过这个机房变成很干净的水。我自己看了这些项目，我想韩市长看了以后感受一定更强。就在这一次世博会包括各国家的自建馆、还有城市最佳实践区里头，您看到哪些是属于上海在未来十年变成世界最大人口城市时，特别值得学习的项目？<br />
<br />
韩正：这次城市最佳实践区给了我们很大的启发。因为城市最佳实践区把过去在城市发展过程中间，或者讲城市化进程中间，人们不断探索出的一些成功的经验进行了回顾，然后把它展示出来。我觉得这个是很有意义的。您知道世界博览会，它历史上都是以工业产品创新的一些成果来展示。这一届世博会不一样，是以城市为主题。城市的产品是什么？马上就会提到这个问题。以城市为主题，而且世博会的理念是面向未来的，就是世博会它并不是回顾过去，或者讲回顾过去所取得成就的展览，它是面向未来的博览会。如果它是要回顾过去，也是给人们一种对于未来的想象。这些理念、这些案例，对于我们未来城市发展是一种很好的启发。因此，这一次我们讨论城市最佳实践区，对于未来城市是什么样子，会有很多想象。我觉得有一点，未来城市的发展一定是在过去成功实践的基础上不断发展的结果。在全世界城市化发展的进程中，中国毕竟历史不是很长。城市发展他有一定规律，我们中国在城市发展中间，有本国的一些特点，自己的文化特质，自己的人文特点。城市让生活更美好，或者讲城市发展的一些延续，一定是有规律的。因此，在这些规律里面，他透视的是什么？我们就是想通过城市最佳实践区，使人们在面向未来的城市发展中共同获得一些启发。这一次，我们就是特别把这些案例，而且是面对城市发展所遇到的困难、矛盾、问题已经有了很好回答的案例，比如刚才讲的环境问题、交通问题、贫困人口问题等等，在这些案例中就体现了一些我们可能寻找到的面向未来城市发展的答案。我觉得这是一个很好的创意，因此在全世界进行了报名。<br />
<br />
陈文茜：你们当时是怎么挑选的？<br />
<br />
韩正：当时就是让全世界的城市报名。这里有个故事。当时叫城市实验区，不是实践区。当时我们提出实验区的初衷，还在申办之际，是2002年12月申办成功以前，我们的规划里面叫实验区。我想，这个实验区就是未来的实践。到2002年12月申办成功以后再进一步讨论，恐怕实验还不如实践。因为已经做好了对未来有启发的案例，可能更精彩，更容易被人们所接受。因此就提出一个城市最佳实践区的概念。实践区就多了，有成功的实践、有不成功的实践、有各式各样的实践。也有你认为成功的、我认为不成功的实践。我们就叫城市最佳实践区。所谓最佳是什么？是公认的、是好的，好的中间还是最佳的。我们有一个国际委员会，因此，就首先由国际专家委员会来进行遴选。它都有标准，都有一些理念，然后就向全世界所有的城市发出邀请，欢迎大家提供你这个城市的案例。很多城市来了案例，当时我们收到的是30多个国家和地区、80多个城市的1000多个案例。遴选到现在就是有15个实物案例，加上展示的案例。这样，就成为今天这样的一个世博会正式的城市最佳实践区。这个实践区里给我们未来城市发展的启示是很深刻的。<br />
<br />
