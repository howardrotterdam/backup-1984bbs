													<h2>杨恒均 ： 在CCTV和CNN上检阅国庆大阅兵有感</h2>
						
						
																											<div id="postmessage_267308" class="t_msgfont"><a href="http://yhjj.bokerb.com/?do=blog&amp;event=view&amp;ids=80108" target="_blank">http://yhjj.bokerb.com/?do=blog&amp;event=view&amp;ids=80108</a><br />
<br />
今天比平时起得早，爬起来一看，十一点半，惊慌失措，难道我错过了六十周年国庆大阅兵？随即想到身在异乡澳洲，悉尼时间比北京早两个小时，北京此时正是九点半，大阅兵尚未开始。我打开电视机，不停换台，CNN，FOX，BBC，从平洋岛屿的海啸到印度尼西亚的地震，都是鸟语新闻，换来换去，就有些紧张了。没有装中文电视，天啊，莫非要错过国庆六十周年大阅兵？<br />
 <br />
赶快打开笔记本电脑，通过锐推向网友发出呼叫，不到一分钟，十几个推友答复我，并给了连接：中央电视台的网址。打开一看，喜出望外，“直播”啊。于是，我一边看电脑上的CCTV，一边看电视上的CNN。CCTV直播已经开始，蓝天白云，北京沉浸在一片颜色的海洋和欢乐的气氛之中，我真想如诗里写的那般，插上翅膀，让心儿飞向北京天安门……<br />
 <br />
“死亡总数已经达到74人，目前还有一百多位受伤，失踪的多达……”这是大屏幕电视上的CNN报道，“美国总统奥巴马宣布小岛为灾难区，澳洲政府也表明，随时驰援……”<br />
 <br />
两个屏幕上报道的反差如此之大，这一刻，我还真有些时空倒错了，灾难发生在遥远的别处，我们只有挡不住的欢乐，是历史已经结束？还是时间才刚刚开始？东方的巨人真的要崛起了？我自豪，我激动，为了一会能够连续观看国庆阅兵，我再次冲向厕所……<br />
 <br />
十点到了！这时CNN停下了新闻报道，画面立即切换到北京天安门广场，我一看，好家伙，硕大无比的屏幕右上角打出了“直播”的字样。我家的电视可是超大平面，在这上面看大阅兵，可比在屁股大一点的电脑上看带劲多了。 感谢CNN，感谢CNN，让我直接从电视上看到实况直播：在我的记忆中，CNN这些年几乎连美国的国庆也没有实况直播过！<br />
 <br />
开始了，威严的仪仗队走来了，啊，激动啊，真威风……“现在让我们看一下北京的群众，噢，他们在哪里――”画面上是仪仗队踏步，我却听见CNN播音员讲其他的，在谈中国问题，还扯到一些上访群众，中国面临的问题……我考，你烦不烦啊？阅兵不光是看的，知道吗？还要听啊，那整齐的铿锵有力的步伐，比画面更能震撼人心……你这样七八嗦的，我听不见啊。于是我稍微调小了电视音量，打开手提电脑的音量，CNN播音员节外生枝，我不如看咱中央电视台的直播――<br />
 <br />
可是，奇怪啊，电视上仪仗队已经走到金水桥了，可电脑上的仪仗队才刚刚出来，怎么回事？询问网友，中央台不是直播吗？网友回答了，要晚一会，大概30秒吧――<br />
 <br />
我这才长了见识，这也叫“直播”？问题在于，如果没有大电视CNN，我也无所谓，可是有了，两个都在我面前摆着，我怎么可能把眼睛从真正的直播转到晚半分钟的“直播”？即便我喜欢电脑上字正音圆的汉语解说，可如果我已经瞥见了电视上的方阵，还会在电脑上再看一遍？<br />
 <br />
我告诉网友，我这里比他们早一些看到下一步发生什么事，我说出来完全是无意的，结果，好几个网友竟然说，啊，羡慕你。羡慕我什么？早了半分钟？早了半分钟，或者说能够看到真正的“直播”，有什么值得羡慕的？这是我今天思考的第一个哲学问题。<br />
 <br />
这种空前绝后的历史事件，早半分钟的意义，或者说真正“直播”的意义非同小可，也许就能见证历史。可晚了半分钟，你只是在看文艺表演。为啥？因为，如果真有历史事件发生，那些搞“直播”的编辑会让你无法看到的，你虽然只晚了半分钟，但你看到的几乎都是人家编辑允许你看到的，就这么简单。<br />
 <br />
从这个意义上说，我从画面上看到每一个笑容，都是如假包换的真实，而我的十三亿同胞看到的，只是CCTV编辑们愿意然你看到的“真实”。也许到最后，我们看到的内容一模一样，但作为人，我们其实并不完全一样。这个哲学问题有点绕口，你想不通就不要想了，别和我较劲。<br />
 <br />
威严雄壮的方阵把我从这个思考中拉了出来，哈，太威风了，太牛了，我马上在网络上发表即时评论，可有网友随即打字回来，什么呀，没有看到啊。――啊，我感慨万分，虽然我在远隔太平洋和印度洋的南半球，可实际上，我比此时此刻坐在中国大陆的13亿人民离北京更近啊，至少，我比他们更快地看到了阅兵的方阵雄壮地走了过来……这一刻，我才深深地感到，作为一名侨居在海外的华人，我有什么理由不比我的大陆同胞更爱国呢？至少我的心儿离北京更近――耶……<br />
 <br />
中央电视台的镜头从空中鸟瞰拍摄天安门周围的情景，这个画面只有中央电视台有，CNN也是借用中央台的，所以这个画面是同步。但长了一双小而锐利的双眼的我，在随着中央电视台的镜头扫了两眼后，就发现了严重问题。于是，我立即在网络上打字：立即停止空中拍摄！请立即停止空中拍摄！！我恨不得打开秘密发报机，发出莫斯密码：老杨头呼叫北京，请CCTV立即停止空中拍摄，特别不要拍摄天安门附近楼房和街道的鸟瞰图……<br />
 <br />
网友不耐烦了，问，老杨，你发神经啊，你怎么一看到士兵就想起战争年代？怎么不能拍摄了，北京高楼大厦，街道整齐……<br />
 <br />
我打断网友说，哎，你真以为我老糊涂了――不，我是爱国心切啊。因为我从那些镜头里看到了不和谐的东西，非常不和谐，我担心外国人过一会也会看出来，于是又要借机生事、大造舆论啊。你看看那些从空中拍摄的北京城，且不说举行仪式的长安街上，就连附近的多条街道，上面不但没有车辆，竟然连一个人影也没有，还有那么多高楼大厦，不要说屋顶上没有一个观摩的人，所有的门窗都紧闭……同胞啊，你知道我参加过多少次国外的国庆游行吗？光美国的，好像就有三次，除了游行主街道，旁边到处都是人民，还有楼顶屋顶上啊，可从CCTV拍摄的画面，北京怎么好象在演“空城计”似的？<br />
 <br />
我是担心别有用心的CNN播音员突然发出这样的质问：庆祝中华“人民”共和国成立60周年，可“人民”到哪里去了？<br />
 <br />
“请购买XX人寿，让你的孩子有一个光明的未来……”正在我低头打字的时候，大屏幕电视里传来CNN的英文声音，“XX牌卫生巾……”天啊，CNN竟然插播广告，我当即松了一口气，好在他们没有注意到“空城”的北京，不过，扫了两眼他们的广告，随即又紧张起来，我的妈呀，要是被国内愤青看到CNN竟然在直播我们国庆庆典的时候，插播这些无聊的广告，还不把他们砸了？谢天谢地，国内人民被保护起来了，他们看不到CNN，他们只看CCTV――那是他们出钱供养的电视，当然在这种场合，是不用插播广告的……<br />
 <br />
女兵来了，女兵来了，多么雄壮，多么整齐，多么美啊……你能够想象，除了北朝鲜外，还有哪一个国家可以组织这么庞大却整齐划一的大阅兵？<br />
 <br />
想到这里，我开始了今天的另外一个绕口的哲学问题：大家夸奖这种大阅兵时最常用的词儿是：整齐划一，意思是这么多人，行动起来，竟然好像“一”个人似的……于是，我随意在电脑上打了一句话：如果我一个人到天安门去走步，会不会更“整体”？我的话音未落，一个网友说话了：一个上去也很难说的，你能保证每一步都走得一样？<br />
 <br />
这句话引起了我的思考，那么多人上去，走得整齐，于是我们表扬他们好像“如一”――象一个人一样，那么，如果是一个人上去，自然不用像谁了，因为他本来就是一个人。这样我们就无法使用“整齐划一”这个成语了。<br />
 <br />
对大阅兵最大的褒扬就是“整齐划一”，可见最美的还是那个“一”，也就是独立的个人。这么多人都是一个又一个的个人组成的，当我们看到他们如何整齐的时候，我们只是在惊叹，他们竟然一模一样，他们竟然一点差别也没有……这时，我们谁也没有想到，这些方阵里的每一个士兵都和我们一样，吃饭穿衣谈恋爱，转业之后可能会去申请保安的工作，然后老板说，走两个正步让我看看，那个曾经参加过国庆大典的士兵于是走了两步，老板说，算了算了，一点也没有那个感觉了，你去守大门吧……<br />
 <br />
我的思绪已经离开了大阅兵，陷入这个哲学问题了。集体主义没有什么不好，但却屡次被极权政府利用，给自己的国家，甚至人类带来灾难。其实，就是因为当我们在群体意识和集体主义忽悠之下惊叹大家竟然能够“整齐如一”的时候，却忽视了那组成集体的一个又一个的“一”。<br />
 <br />
其实，我也不能免俗。例如据说，北京国庆期间不但没有不和谐因素，甚至连小偷都没有了。听到这样的消息，我很惊讶，想说点什么，却又说不出来。是啊，相对于这样轰轰烈烈的庆典，整齐划一的大阅兵，那些势单力薄的小偷和一两个不和谐人，真是显得那么的不合时宜，那么的微不足道。你看，我也被集体主义感染了。<br />
 <br />
看完大阅兵，等到“群众”上场的时候，我就离开了（因为要去游泳减肥）。我之所以把“群众”用引号打起来，那是因为他们只是选出来的群众的代表，所以本人加上引号以示区别。记得在外国参加国庆游行，连我这个外国人也可以跟住大队人马，随波逐流，浑水摸鱼，那样的庆典中出现的群众，才不用打上引号以突出他们的“选民”地位。（注：这里的“选民”是“被选上的民众”，而不是有权投票的那个“选民”）<br />
 <br />
这次同时从CCTV和CNN观看大阅兵，发现CNN的报道基本上还算公正的，当然，多次插播广告，搞得我很郁闷。特别是在方阵走上来后，CNN竟然转到别的新闻上了，可等到那个穿红色衣服的女兵方阵上来时，它又立即转回到北京天安门现场，大概半分钟不到，等到女兵消失了，它又去播送其他新闻。虽然我也喜欢看女兵（而且还向往有打听她们是哪个部队的，改天去慰问慰问），可你CNN也这么明目张胆，就有点伤我的民族自尊心了……<br />
 <br />
CNN在播放的中间插播一句话：中国向何处去？谁知道？我看到他们又杞人忧天没事找事，就骂了一句，并顺手写道：我不是早在博客里写了？看起来CNN那帮人不看我的博客。<br />
 <br />
第一次在电视中看到北京的天空竟然如此美丽，那么蓝，上面还漂浮着几朵像画上去的白云，简直让人不敢想像，和澳洲的天空一样了……<br />
 <br />
最后，我要对举办这次大阅兵的领导同志们说一声辛苦了，并谢谢你们让人民（包括我这个小民）从电视上检阅了“人民”的军队……<br />
 <br />
 <br />
杨恒均 2009/10/1 国庆节六十周年于澳大利亚悉尼</div>

							
							
							
							
