2肥皂节目受热捧<br />
<br />
现代人脑子好使，但是偏不愿意使，对电视节目的选择也都是喜爱那些不用动脑子的。所以愚蠢搞笑，不动脑子的娱乐节目、肥皂剧霸占了荧屏的黄金时段…<br />
<br />
<br />
3词汇单一化<br />
<br />
现代人用动词不动脑子，一个“搞”字可以胜任所有动词的作用。听听如下的对话：“咱们准备搞一个活动，你帮着搞个场地吧？什么？搞不到？搞什么搞啊……”<br />
<br />
<br />
4“经济文盲”遍地走<br />
<br />
现代社会，文盲不多了，但经济文盲还是屡见不鲜。典型特色易被忽悠，专家说啥信啥。所以会有那么多说错话的“专家”，而且他们说错了我们还会继续信任他们…<br />
<br />
<br />
5官员弱智化<br />
<br />
总是念错字、乱用成语，而且说的话下属都不信。似乎中国官场形成了这么一种常规：领导说什么不重要，下属不信，领导自己也未必信，开会基本就是走过场，领导渐渐成了无用的摆设…<br />
<br />
<br />
6词性变异化<br />
<br />
骚、牛这些10年前很难听的脏字，如今在小S、周杰伦的洗脑之下成了流行词汇。港台流行恶俗文化对大陆文化的侵蚀是潜移默化的。这姑娘真骚，这小伙超拧Ｔ经的脏词被无脑地洗白了…<br />
<br />
<br />
7恶搞成风<br />
<br />
在传统观念看来，罗玉凤这种类型的女孩无论如何不会成为大众红人。但在笨蛋社会却可以。如果说大话西游式的恶搞还有些微文化味在里边，那现在凤姐成为大众明星就是无底线恶搞的标志…<br />
<br />
<br />
8拿无知当个性<br />
<br />
不怕笨，就怕笨还不自知，拿无知当个性。现在是个追求个性的时代，这本无可厚非。但在人们普遍缺乏常识，喜好哗众取宠的大环境下，很多貌似个性的举动，其实恰恰是无知的表现…<br />
<br />
<br />
这是一个 “人精 ”遍地的笨蛋社会<br />
<br />
<br />
1“没有什么是值得思考的？”<br />
<br />
大前研一曾描述日本大选时盲目投票的景象。他对此的注解是：只要稍加思考，就能理解有些事情显然是很荒谬的，但是越来越多的人甚至对此都毫不质疑。在现在的中国，投票、灭灯类节目异常火热，究其原因就是因为这种选择只需要遵循快感而不需要经过大脑。<br />
<br />
<br />
2懒于动脑而轻信砖家<br />
<br />
日本曾有一段时间流传“早上吃香蕉有好处”的养生法则，于是超市的香蕉被抢购一空。我们突然想到最近才刚刚偃旗息鼓张悟本和他著名的生茄子绿豆理论，大爷大妈把张医生奉作神明，没人质疑这次集体疯狂。懒于动脑而轻信砖家，笨蛋社会怪状之一。<br />
<br />
<br />
3大学生名不副实<br />
<br />
“越来越多的大学生名不副实”也是判断“笨蛋社会”的一个重要衡量。日本陷入“笨蛋社会”时，大学排名和教育质量都在下滑。而在当下中国扩招后又培养了多少“快餐大学生”？他们在学校中混了四年，没有思考和学术能力，毕业的时候才发现找工作都成了神话。<br />
<br />
<br />
4经济繁荣和幸福度体现集体智商<br />
<br />
大前研一书中表示，一个国家集体智商高的时候是经济繁荣的时候。反观中国，虽然每年统计局告诉我们GDP不断飙升，但刨去那些不靠谱的水份，再摊成人均GDP却没什么大变化。况且这些财富牺牲了人们的幸福度，每天活在焦虑和奔波中的人，哪有时间顾及智商。</div>

							
							
							
							
