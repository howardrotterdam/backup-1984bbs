<br />
1、德国国防军没有明确的效忠对象，德国职业军人（国防军）从来没有信仰过某个个人。德国传统的军官团的信仰只是为祖国服务，为人民服务，谁做皇帝谁做总统都和自己无关。这就是为什么希特勒不信任国防军将领，也不信任国防军而去发展武装党卫军。<br />
<br />
党卫队是效忠于纳粹国家社会主义工人党和希特勒党魁的，希特勒给它的题词是：“党卫队，你的荣誉便是忠诚。”它最终变成了希特勒和纳粹党的暴力工具，在党卫队的条例中没有人性而言，只有无限忠于和不怕死亡。<br />
<br />
2、德国国防军的枪口从来没有指向德国人民，即使是在国内出现希特勒暴力夺权的时候，德国国防军仍然守在国境线上，紧紧盯着法国人正在修建的马奇诺防线方向，在德国国防军军人眼里看来，沐浴龙血的日耳曼斗士的长剑是不应该挥向自己的兄弟姐妹的。<br />
<br />
