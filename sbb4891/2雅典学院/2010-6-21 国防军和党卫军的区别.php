<br />
德国党卫队在整个二战中所犯下的滔天罪行是如此之多，以致于盟军士兵得到命令，凡遇到党卫队成员，一律就地处决不用上报。因为党卫队员不是军人，所以不受到军人战俘的待遇。战争结束后，曾经有人认为党卫队也是军人，<strong>时任纽伦堡军事法庭首席审判长的那瓦伦斯淡淡的说：军人不会对内镇压，不会对平民屠杀，不会忠于某个政党或个人，所以，他们是匪徒，不是军人”，因此，在纽伦堡军事法庭审判结束后，所有战俘营里关押的党卫队员被挑选出来，全部就地处决</strong>！！！<br />
