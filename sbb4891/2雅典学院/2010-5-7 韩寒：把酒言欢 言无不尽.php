标签：杂谈&nbsp; &nbsp; &nbsp; &nbsp; <br />
我接受过一些采访，外媒和内媒是不一样的，虽然他们有的时候可能会问出一样的问题，我给出一样的答案，但是最后见报的内容也是不一样的。相对来说，外媒的问题更加的直接，、有些问题直接到你没有办法回答，因为你如果回答了一次，那估计你以后只能永远接受外媒的提问了。我会诚实的告诉他，这个问题我不能回答你，不是我不愿，是我不能，回答这个问题需要付出太大的代价，而且暂时是无谓的代价。但我又不愿意说假话，所以我选择闭嘴，但你可以保留我的问题，我觉得这是个好问题，你就说，被采访者他不敢说。<br />
请原谅我的懦弱。<br />
 <br />
说实话，在回答一些内媒问题的时候，我反而会说更多，因为我知道，无论我说多少，就是我和记者本人在聊天，经过了自我审查，最后见报的内容一定是能见报的。面对外媒，我反而会表述更多的希望。刚刚回答了一个加拿大媒体的采访，觉得还挺有意思和代表性，所以摘了几个问题，有一些改动。<br />
 <br />
问：你怀念谷歌么？为什么？<br />
答：事实上，我一点都不怀念谷歌。谷歌就像一个姑娘，有一天她跑过来说，我要离开你。我说，不要这样亲爱的。让人伤心的是，最后她还是离开了。但是我发现，其实当我想上她的时候，我还是随时能上她。唯一的不同就是以前我上她的时候能从她身上搜出胡萝卜，但是现在，我问她，胡萝卜呢，她就嗖一下不见了。<br />
 <br />
问：如果你有机会定居海外，比如加拿大，你会不会去，为什么？<br />
答：我不会愿意的。加拿大是一个很美丽的地方，生活舒适，环境一流，人均GDP很高。虽然我的国家总GDP很高，但是人均GDP还是很低，而且我们为此付出了很多代价，我的家乡很多地方污染严重，腐败官僚，有的时候他进一步，有的时候他退两步，但是我始终想留在我的家乡，看着他或者帮助他多进两步，毕竟那里是我的家乡，就像你们再美好，你们也无法翻译和理解我上一个问题的答案一样。<br />
还有一个原因是在我的祖国，我的身边是中国贪官，如果换了一个国家，发现身边还是不少中国贪官，我肯定崩溃了。<br />
 <br />
问：其他国家包括加拿大，应该如何看待中国的强大和他在国际上发挥的作用。<br />
答：这个问题我觉得你们应该去问我们的官员和领导，但是我也可以告诉你们，你们只需要参考他们对其他国家关于这个问题的回答，然后改一个国家名字就行了。你可以用所有已有的答案去套所有现有的提问。<br />
 <br />
问：你如何评价中国的极端敏感？<br />
答：这个过敏，我实在不能评价。我只能说到这儿，司法独立才是真的，但是我们国家，司法是不能独立的，因为司法独立不符合国情，什么是国情，国情就是只管赚钱，靠什么最容易赚钱，靠权力，司法独立会制约权力，你制约了权力，你让那些有权力的人以及其家属怎么赚钱啊，所以，司法独立不符合中国国情。<br />
 <br />
你希望你的国家是什么样子的（这是一个日本媒体的提问）。<br />
答：不通过房地产或者卖地，不通过低端的加工业，一样有高GDP，而且是人均。好人不翻墙，坏人进监狱，有影响世界的文化，有别国模仿的文艺，一样干净的环境，一样自由的空气，看着被关进笼子的权力，把酒言欢，言无不尽。</div>

							
							
							
							
