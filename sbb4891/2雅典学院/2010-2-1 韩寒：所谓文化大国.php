<strong>多谢48楼Len的文字重排</strong><br />
第二次来到厦门，然后这里的空气很好，难怪大家都喜欢散步啊。嗯。。。刚才我听邓老师说了一些，然后关于爱国主义的一些东西，那我想到了两句话，我是之前看到的，但是别人说的，不是我说的。第一句话是---爱国主义是流氓最后的庇护所。第二句话是---真正的爱国主义就是要保护这个国家，让这个国家不受到任何的迫害。<br />
&nbsp; &nbsp; 呃。。。然后今天我也准备了一些说的内容，带了一个稿纸，这是为了约束我自己，不要大家受到什么迫害，我怕我满嘴跑火车，开始了啊：<br />
&nbsp; &nbsp; 各位领导，各位老师，各位同学大家好！大家知道中国为什么一直成不了一个文化大国吗？因为在我们大部分的讲话的时候，各位领导永远是放在第一位的，而各位领导都是没有文化的。他们是惧怕文化的是审查文化，但是呢，他们又能够控制文化，所以说这个国家怎么够成为文化大国呢？各位领导，你们说呢。<br />
&nbsp; &nbsp; 其实中国是有成为一个文化大国的潜力的。我给大家讲一个故事。我主编一本杂志，那么现在都没有出版。然后呢，宪法上有规定啊，每个公民都拥有出版的自由，但是呢我们的王法又有规定，就是领导有不让你出版的自由。&nbsp; &nbsp;<br />
&nbsp; &nbsp; 这个杂志呢很多地方在审查上遇到了很多问题，里面有一副漫画，漫画里面是一张图，主人公是个男的，他没有穿衣服。当然这就是不可以的嘛，因为相关的法律法规规定就不能露出那个阴部，在那个公开的出版物上。但这是我认可了，我觉得没有问题。所以特地把那个杂志特别大的一个LOGO啊，就放在它那个不合法的那个部位。然后突然之间就是出版社的审查人员就告诉我说，诶，这个不可以，你把这个人的中间这个地方挡住了，说你这是在暗喻&quot;党中央&quot;。<br />
&nbsp; &nbsp; 我的反应和大家一样啊，我被雷到了。我当时脑子里就在想，朋友，把你这个惊天地泣鬼神的想象力啊用在文艺创作上，然后而是用在文艺审查上那该有多好。通过这个故事我想告诉大家，其实大家都是可有想象力的啊，但是很多事情我们只能想，我们不能，不能去做，不能写，甚至有的场合不能说啊。呃。。。我们的限制太多了，这是一个限制级的国家。在限制级的国家里怎么可能产生非常丰富的文化呢。是吧。我已经算是一个自我限制很少的一个同志了，但是在我落笔的时候呢，我就会情不自禁地想，这个警察不能写，领导不能写，政策不能写，制度不能写，司法不能写，奴隶制不能写，西藏不能写，新疆不能写，集会不能写，游行不能写，黄色不能写，封杀不能写，艺术不能写，啊，高雅我也写不出。谢谢，我真的写不出高雅，我又不是余秋雨。<br />
&nbsp; &nbsp; 在网路上发表的一些文章啊，尺度已经算是比较大的。有很多写剧本的朋友，比如张爱玲这些写剧本的朋友，包括象类似宁财神啊写一些话剧，拍一些电影剧本和电视剧，他们非常的痛苦。在这样的一个文化环境下，就我一直在想，如何去成为一个文化大国，除非全世界就只剩下中国、朝鲜还有阿富汗。朝鲜是文化禁地，大家都知道。然后阿富汗是因为国内的局势还搞不清楚，他还顾不上，但纵然这样，他们都已经有作家写出了《追风筝的人》。当然比较遗憾的是这也不是在阿富汗出版的，但我想，一旦阿富汗搞清楚了，也不是没有可能去超过中国。<br />
&nbsp; &nbsp; 呃。。。我们所谓的在国际交流上不能赞扬一些四大名著和孔孟之道，说穿了，这样就像相亲的时候女方问你有没有钱，你说你祖宗十八辈上有钱。这是没用的。这个悲剧的造成，我觉得，和大家没有任何的关系。虽然说，通往朝鲜的道路，是由每一个沉默的人铺就的。但是，这一方面，我们当然要比朝鲜强很多啊。因为大家也都知道朝鲜是什么样子的。另外一方面呢，我相信在座的大家，其实很多人，大家并不沉默啊，大家只是被和谐了。<br />
<br />
最后说回到我们的屏蔽词库啊。一个屏蔽词库里的词越是多，这个国家的文化可能就会越是弱，但我们ZF会给大家很多的解释。他们会告诉你，我们这么做是为了保护青少年，是为了社会的稳定，文化是自由的。所以他们有权屏蔽任何危害青少年，破坏社会稳定的资讯和文化。但是如果你认同了，迟早有一天你会发现，你在控诉你的遭遇的时候，他们会将你屏蔽，罪名是破坏社会的稳定。到最后，凡是不利于统治阶层的，不利于他们获得利益的言论，都是破坏社会的稳定，都是危害青少年。如果我们当时容忍了绿霸花季护航的话，很快我们就会看到绿霸花甲护航。到那个时候就不光光是文化的东西了，所以同学们，我们不能让这一天的到来，否则在以后，在若干年以后，在你的孙子们，通过卫星接收到的电子课本的历史书上，我们都会是笑料。<br />
