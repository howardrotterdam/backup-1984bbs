													<h2>致中共无期囚徒王炳章博士公开信－－辛灏年</h2>
						
						
																											<div id="postmessage_317375" class="t_msgfont">王炳章博士： <br />
<br />
　　此时此刻，我不想向你致以任何世俗的问候。我深知，对于一个身陷“巴士底狱”的正义者和不幸者，对于一个早已不害怕将牢底坐穿的民主革命家来说，你不需要这个。因为你不会崩垮的意志，从未中断的追求，已经使你无需再汲取任何世俗的力量，来面对那个酷似欧洲中世纪教权统治的“北京洋教政权”了。仅仅是在几天前，你面对“邪教审判”，曾高声呼喊“中国的民主一定能够胜利”，亦已证明我对你认知的正确。 <br />
<br />
　　然而，我却要给你写下这一封信。我要告诉你，当你被当代、甚至是历史上最黑暗的统治机器判处终身监禁的消息传来时，当全世界正派的华人，都不得不陷入愤怒或痛惜之中的时候，在纽约“抗议中共陷害王炳章博士”的大会上，我，一个根本不是民运人士的文化人，你的一个最普通的朋友，一个熟人，究竟为你说了些什么？ <br />
<br />
　　我要告诉你的是，我几乎是含着眼泪，向着你稀少的战友，众多支持你的华侨，所说的，实在是已经在我的心里埋藏得太久太久的话。这些话，倘使你如今不是身陷囹圄，恐怕一个学者专家的清高和矜持，一个普通朋友害怕是非的恐惧心态，一个知识分子常有的瞻前和顾后，依然要逼迫我欲言又止。 <br />
<br />
　　王炳章博士，我在那个声援你的大会上承认，我是在听够了你的坏话、甚至是听够了对你的糟蹋之后，才有“缘份”与你相识的。但我承认，当你第一次和朋友来看我时，我还是感觉到：这人，从他的举止、谈吐、修养和风度来看，还真有点“革命领袖”的作派呢。 <br />
<br />
　　此后，我们见面的机会虽然并不多，但我还是渐渐地了解了你思想的脉搏，你那传奇式的经历，令人不平的遭遇，还有，就是你的意志──一种怎样颠扑不破的意志啊！ <br />
<br />
　　所以，我在纽约营救你的大会上所说的第一个问题，就是：王炳章具有许多知识分子所没有的卓越品格。 <br />
<br />
　　因为我将心比心，将己比你，便使自己深深地感觉到：你思想胆大，行动也胆大；而我则是“思想胆大，行动胆小”。就像我只敢辨识“谁是新中国”，证明孙中山先生创建的中华民国才是一个真正的新中国，指斥毛泽东的“中华人民共和国”不过是民国史上的一段“乱世和恶世”，是一次“全方位的专制复辟”──虽然是在“革命的名义”之下，但是，我却不敢像你那样，一旦真理在手，就会立即投身到号召“重建中华民国”的伟大民主革命实践中去…… <br />
<br />
　　你绝不害怕是非缠身，敢于脚踏惊雷；而我面对是非，总是望而怯步。就像我在读完了你题名为《中国民主革命之路》的书稿之后，虽然相当佩服，却只敢婉言谢绝你请我写序的要求，因为我害怕被卷进可怕的“民运是非”之中去。而你，虽然是非缠身十数年，却始终毫无惧色，照样在你那个“背景复杂、是非连天”的阵营内外，横冲直撞，只为了信念和明天，而奋斗不止。 <br />
<br />
　　你忍辱负重，百折不挠。而我却带着一个文化人似乎荡涤不尽的自尊和清高，难免文人的牢骚和一个知识分子的不平心态。为那个不想要中国的“台湾中华民国和国民党”，对我的长期打压和肆意诬蔑，多少怀有一些怨悱之心。（1）然而，在王若望先生的追悼会上，你所遭遇的“相煎太急”，虽然令人扼腕，事后，在许多朋友都在为你深感不平之时，你却满含泪水，一言不发。此后，也从未表示过一丁点怨恨和不满。就是在那个晚上，我才像是突然地意识到：我虽然听够了别人说你的坏话，甚至是听够了别人对你的糟蹋，但是，却从来没有听到你说过任何一个人的坏话，糟蹋过任何一个人。而当我将这个突然的觉悟，告诉给你的许多朋友时，他们都好像是“恍然而大悟”── <br />
<br />
　　炳章，我要告诉你的是，王若望先生的未亡人、我的羊子师母，已经在那个营救你的大会上，向你表示了深深的道歉，明言了她当时实在没有力量来抗拒的“苦衷”；黄景贤先生也当即走上讲台，证明王炳章博士确实没有说过任何人的一句坏话。会后，许多人都聚在一起，为此嗟叹不已。王炳章博士，你难得的品性和人格，终于经受了岁月的煎熬，上帝还是公正的！ <br />
<br />
　　今天，我是多么希望你的“那些”战友们，都能够对你捐弃前嫌，积极、真诚地参与到海内外营救你的活动中去，并重新共同为中国的民主自由而奋斗！ <br />
<br />
　　我在大会上所说的第二个问题，就是王炳章博士与二十年中国民主运动的历史关系。 <br />
<br />
　　我首先就开门见山地宣称：你是当代海外中国民主运动的开创者，就是将你称为整个当代中国民主运动的开创者之一，也毫不过份。因为，无庸讳言的是，一九四九年之后，第一个公然举旗反共要民主的，就是你。这和那些出于对共产党的形形色色“忠诚”，要给那个专制复辟政党以各种各样“建言”，而反遭迫害者，岂但不能“同日而语”，而且不能“同性而论”。 <br />
<br />
　　其次，我毫不讳言地指出，二十年来，你王炳章算是坚守了真正的中国民主运动、特别是海外中国民主运动的“两大原则”： <br />
<br />
　　一个原则就是你只做中国的民运，绝不做美国和其它任何一个国家的“中国民运”；也绝不做那个不认、不要中国的“台湾中国民运”。并且是不给钱不做，给钱也不做！八十年代，你曾向支持中国大陆海外民主运动的蒋经国先生说：“你是你，我是我。你们支持了我们，我们也要保留批评国民党的权力。”九十年代，面对那个不认中国、不要中国的李登辉国民党，你曾义正辞严地告诉他们说：“我们有骨气、有理想的大陆民运人士和知识分子，是不可能充当你们的特务和线人的。对不起，这种交易，我不能做。”（2） <br />
<br />
　　你坚守的另一个原则，就是那两个“绝不可以改变的民主运动立场”。一是爱国主义的立场，二是爱民主义的立场。为前者，你坚持民主运动就是爱国主义运动；中国的民主运动，就是爱中国的民主运动；而爱中国的民主运动，就只能是追求国家统一，反对分裂国家。于后者，则爱民就必须反共。因为共产专制复辟制度，才是中国人民的死敌。我在大会上介绍了你写的那一篇“究竟应该拥护什么、反对什么”的文章。我告诉大家，在这篇文章里，你实在已经将中国民主运动应有的爱国爱民立场，表述得异常清楚和明白。 <br />
<br />
　　我的话为你赢来了一阵阵急风骤雨般的掌声。是的，人民是知情的，华侨是懂义的。你的“真正的战友们”，也是“心有灵犀，一点就通”的。 <br />
<br />
　　再就是，我在这个营救你的大会上，对十四年前那个太大的“民运是非”，直率地提出了看法。我认为：“一九八九年春天的海外‘倒王事件’，是整个中国民主运动由盛而衰的分水岭”。 <br />
<br />
　　虽然这个事件既为中共一手策划，又为中共特务一手谋划和发动，但因它利用了海外民运内部的意见分歧，和你在民运工作中难免的缺失，所以，至今没有人敢于在此大是大非的问题上，进行认真的反思。而我，作为一个非民运人士，并且是历史学者，却认为：由于“倒王”事件，已经在相当程度上和相当范围内，极大地扭曲并败坏了你的形象，甚至直到今天，它都在直接地影响着海外民运阵营对你义不容辞的营救工作，所以，我才决心学习你“不怕是非缠身”的勇敢精神，针对着这一“大是大非”，向大家提出了四个问题： <br />
<br />
　　一是“倒王”之后，海外中国民主运动是不是越来越团结了？二是“倒王”之后，海外中国民主运动是不是越来越有人气和力量了？三是“倒王”之后，海外民主运动是不是更叫共产党害怕了？四是“倒王”之后，海外中国民主运动是否越来越得到了广大华侨和留学生的支持、参加和援助了？ <br />
<br />
　　如果对上述四个问题的回答，全是正面的，则“倒王”即便是错的，那也没有什么大不了！因为，如果王炳章一个人受了冤屈，但整个海外民主运动却获得了长足的进步，那就让王炳章委屈去吧──我这样说，想你不会生气。但是，如果针对上述四个问题的回答，竟然全部是反面的，那我这个海外民运的局外人，就不得不问上一问，“倒王”事件究竟是对的，还是错的？ <br />
<br />
　　我之所以要提出这个问题，绝不是一时的冲动，而是我思索了很久的一个结果。因为，当我翻阅了一九八九年之前的全部《中国之春》杂志之后，我所了解到的，八九年前海外中国民主运动的构成成份、正确方向、和生龙活虎的状态，实在与我已经亲眼看到的──八九年之后的海外民运，形成了鲜明的历史性对照。就不说，八九年春夏之交的血腥中国，又曾给海外中国民主运动带来了怎样的“历史契机”啊！然而，九十年代之后的海外中国民主运动，其每况愈下的凄凉景状，竟然只能叫人痛彻心脾…… <br />
<br />
　　我知道，我说了这话，便是要得罪人的了。但我的话，实在不是针对任何人而来。因为，十四年前的那一场是非，既然已经造成了如此严重的后果与局面，我们就有理由对那一场大是大非，作一个起码的反思和反醒。这不是为了任何人，甚至不是为了你王炳章，而是为了海外中国民主运动能够回到她的正确道路上来。 <br />
<br />
　　炳章，我要告诉你的是，我的这一段话，并没有赢得一丁点掌声。相反，整个会场，寂静得似乎连掉下一根针头都能听见。不，那不是窒息，而是一场惊心动魄的沉重反思，所必然要有的“无声序曲”…… <br />
<br />
　　而我说的第三个问题，就是我称你是当代中国民主革命的倡导者和实践者。 <br />
<br />
　　炳章，你不要骄傲。因为，这并非是在恭维你。而是在告诉大家，你是在经过了惨淡的追寻，和冷静的思考，并且能够将个人的全部愿望，熔入了国内人民的痛苦意志之后，你才开始了对当代中国民主革命的倡导和实践。 <br />
<br />
　　我首先就向大家介绍了你曾说过的一句话。那就是：“为什么对共产党就不能讲革命？”然后，我便就事论事的地问说：“一个政党，无辜地、残忍地、连续地革掉了数千万民命，并且将我们的国家和民族一再地推进了痛苦的深渊，而我们，无数被戕害者及其后代，无数决心为自由而战的民主战士，在它于八九年那一次公开的血腥屠城之后，便连对它说一声“革命”，便是错误的？甚至是“有罪”的？ <br />
<br />
　　天理安在？！ <br />
<br />
　　至此，我不得不把你的话叙述了一遍。你说过：只要当代那个专制统治者，不宣称永远放弃暴力，并保证不再用暴力来镇压人民及其正当的民主要求，开始信守“和平、理性、非暴力”的原则，人民就永远不能放弃暴力反抗的权利，特别是“革命的天赋人权”！因为，面对一个不用暴力来镇压自己人民的政权，却要使用暴力反抗，诚然是不理性的；但是，面对一个不仅迷信暴力，而且一贯使用暴力来镇压人民的残暴政权，却要口口声声地对着自己的人民，“念经”一样地高喊大叫着“和平、理性、非暴力”，则同样是不理性的，这无非是在为共产党“拉偏架”！ <br />
<br />
　　掌声，响亮的，如同一阵汹涌浪涛般的掌声，轰地便震响在整个会场之上。我知道，那可不是给我的，而是给你的…… <br />
<br />
　　我告诉大家，我和你的交往，仅仅是“君子之交”。至今，你都没有请我吃过一碗牛肉面，而我，至多也就请你吃过一次“印度面包”。但是，我们是在交往中谋求共识，在共识中增进友谊。而作为增进友谊、形成共识之纽带的，也就是那两本书。一本便是你的《中国民主革命之路》，它使我爱不释手；一本就是我的《谁是新中国》，你也确乎是认真地阅读了它。因为，它们，一本是用自己在海外整整二十年的血肉奋斗，才凝就的，志在揭示如何“走通”中国民主革命之路的“真经”；一本则是在国内呕心沥血十数年，才研究写成的，对于历史是非的痛苦辨识。好与坏，有价值和无价值，就都留给别人和后人去评说吧。但在你我之间，这两本书，一本帮助我理解了当代中国民主革命的纯洁理想和正确方式，一本则帮助你看清了中国现代历史的诡谲风云，和一个胜利者精心编制的历史谎言。作为革命家的你，终因蓦然回首，而断然地选择了那一条“既有传承、又有发展”的正确历史道路，也就是孙中山先生曾开创成功、却至今没有完成的中国民主建国之路。自此，由《谁是新中国》所辨识的历史是非，终于在你的心里，展现了当代中国民主建国的明确道路，一曲《重建中华民国》的响亮之歌，岂但使得“谁是新中国”的辨识者肝肠一震，而且，它那嘹亮的呼喊，更是震响在“黄花岗”先烈们那一座座斑驳脱落的石碑上空……倡导者的聪慧，实践者的坚韧，由此而集你于一身。而今，当你深陷“巴士底狱”，就侧身在黄花岗烈士陵园之时，（3）你可听见烈士们的英魂，正在向你呼叫着怎样的期望吗── <br />
<br />
“你要把那牢底坐穿！<br />
“你更要将那复辟了专制的洋教朝廷坐翻！<br />
“…………” <br />
<br />
　　炳章兄，你听真切了吗？ <br />
<br />
　　王炳章博士，我曾不止一次地向你表明：我有明了是非之心，但不敢涉足是非；我有民主革命之识，却不敢身体力行；我有对真正反共民运的同情和支持，又总是踟蹰、徘徊在它的惊波险浪之外──文人的毛病啊！知识分子的苟且啊！ <br />
<br />
　　但是，在这个营救你的大会上，我却要真正开始学习你的精神，勇敢地讲出真话，大胆地匡正是非，责无旁贷地宣称了自己对历史的期望，那就是── <br />
<br />
　　在孙中山先生的旗帜下，走王炳章博士的道路，将伟大的中国国民革命推向最后成功！ <br />
<br />
　　一阵惊雷般的掌声，真的将我的心和众多华侨的心熔化在、激荡在一起了…… <br />
<br />
