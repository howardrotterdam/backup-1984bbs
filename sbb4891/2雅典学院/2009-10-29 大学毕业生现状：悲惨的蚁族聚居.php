													<h2>大学毕业生现状：悲惨的蚁族聚居</h2>
						
						
																											<div id="postmessage_305540" class="t_msgfont">大学毕业生现状：悲惨的蚁族聚居 <br />
&nbsp;&nbsp;2009-10-29 中国经济时报&nbsp; &nbsp;<br />
　　提要：廉思等人在北京的调查发现，大学毕业生聚居村中，超过85%的人为外地户口，仅15%的人是北京本地户口。廉思对本报记者说，大城市大学毕业生住房困境，实际还指向了城乡二元结构以及高等教育、就业政策等一系列问题。<br />
　　“去年冬天，是我经历过的最冷的一个冬天。”如今在北京一家媒体工作的小赵说，“因为房子里没有暖气，和别人也不熟，屋里很冷，心也很冷。”小赵去年毕业，11月份只身到北京。11月中旬，北京开始集中供暖。但是小赵在昌平租的一个单间，月租200元，公共厕所、公共水房，没有浴室，大约10平方米，最主要的是，没有暖气。<br />
<br />
　　最近出版的《蚁族：大学毕业生聚居村实录》里有更多的“小赵”。新书来自于一项社会调查，是由北京大学中国与世界研究中心博士后廉思组织的。<br />
<br />
　　“蚁族”是指接受了高等教育、从事临时性工作甚至失业或半失业、平均月收入低于2000元、在大城市城乡接合部聚居的大学毕业生群体，年龄集中在22―29岁，大部分大学毕业3年。而他们的居住环境通常是面积狭小、租金低廉、卫生条件差、安全隐患大的出租屋。而光在北京，这个群体的人数就超过10万。廉思估计，在全国，这部分人的数量可能超过100万。<br />
<br />
　　多位专家接受中国经济时报记者采访时认为，大学毕业生住房问题的解决，要涉及户籍制度和房地产政策等多方面的改革。<br />
<br />
　　大学毕业生住房现状<br />
<br />
　　小赵是天津人，山西财经大学新闻专业本科毕业，在到北京之前，她在天津找了一份私企内刊编辑的工作，但“觉得没意思”。11月，她在北京某媒体找到一份工作。尽管天津月入2000元，北京基本工资只有1000元，扣除四险一金之后600多元，“没钱租房子”，但她觉得，这才是自己的本行。就这样，小赵开始了“北漂”，或者进入了她自己所说的“创业初期”。<br />
<br />
　　在没有暖气的民房里，小赵用“小太阳”电热器取暖，但效果不好，盖上两条被子，屋子里依然是冰冷的。小赵说，她的情况还好一些。她的一个同事也住在这个村子里，冬天的时候，屋子里的水都经常结成冰，“敲都敲不开”。<br />
<br />
　　“今年冬天一定要租一间有暖气的屋子！”小赵说，但是直到现在，这个愿望还没有实现。她希望月租能在600元以下，但即使是她现在住的北京近郊昌平，一个不带浴室和厕所的民房，如果装上暖气，月租就达到650元。<br />
<br />
　　也有租金低廉的地方。北京南郊的大兴和通州，1300元可能租到一套二居的普通住宅。还有地下室，月租通常在300多元。此外，就是廉思“蚁族”课题组着力调查的大学生聚居村。<br />
<br />
　　廉思组织的“蚁族”课题组的调查从2008年就已开始，主要是在北京调查北京的大学毕业生聚居村。调查结果显示，北京大学生聚居村中，69.6%的人居住面积在10平方米以下、月租金377.24元。这些聚居村主要在海淀区的东北旺、西北旺、二里庄、肖家河，昌平区沙河镇等乡镇，全部处于城乡接合部，交通便利。这里聚居了10万以上的大学毕业生，其中，5万大学毕业生生活在海淀区西北旺乡的唐家岭，相当于当地村民人口的20倍。唐家岭位于北五环外，紧邻有“中国硅谷”之称的上地信息产业基地和奥林匹克公园，被生活在这里的大学毕业生称为“大唐”。<br />
<br />
　　课题组负责人廉思告诉本报记者，在小月河，通常是七八个人住一个宿舍，像学生宿舍一样；在唐家岭，则是一个楼里，有很多的单间，“像鸟笼子似的”，通常是农民私搭乱建的，七八层的砖房瓦房，都是公共浴室、公共水房、公共厕所；在昌平，是农家乐形式的，小平房，“开门基本是狗，自己生炉子取暖”。<br />
<br />
　　廉思把这些聚居村的生存状态概括为“几小”：“小餐馆”、“小发廊”、“小作坊”、“小诊所”、“小网吧”、“小成人用品店”和低档娱乐场所等等，通常是无照经营。“隐患是非常大，”廉思说，大部分楼房的地基不到半米，“如果原本是四层的楼房，要盖成七八层，他是不会把四层全部扒掉再建的，就在原来的基础上建。那地方要是有地震，那危险性是极大的。一般只有一个楼梯口，如果是起火的话，那真是‘一户失火，千户联营’。”此外，入室盗窃、抢劫斗殴等案件频发，甚至有村民向大学毕业生强行收取10元／月的保护费。<br />
<br />
　　廉思说，课题组下一步将在上海、广州、杭州、西安等地进行调研。他介绍，在上海，三居室的高等住宅隔断成一二十个小隔间，大学毕业生就在这种地方聚居。尽管上海有关部门曾下文不允许将高等住宅隔断，但是事实上规定成了一纸废文。<br />
<br />
　　《蚁族》出版前后，广州、杭州等地的媒体相继与他联系，原因都是：我们当地也有“蚁族”。《郑州晚报》的《郑州“蚁族”讲述奋斗史》说，郑州陈寨、庙李、柳林等地也是大学毕业生聚居村，“在这里，招聘类报纸最吃香，每天早上都是一个个地拿着报纸，奔赴在找工作的旅途中。”<br />
<br />
　　能够租房已经属于梦想<br />
<br />
　　“如果你今天买了房，我今天就嫁给你！”<br />
<br />
　　《蚁族》中，郑章军的女朋友这样说。但实际上，对大多数的大学毕业生来说，买房还是一件遥远的事，能够租到一间普通的住宅，已经属于梦想。<br />
<br />
　　根据麦可思?MyCOS?《2009中国大学生就业蓝皮书》，2008届毕业生就业量最大的前10位城市依次为北京、上海、广州、深圳、杭州、天津、成都、南京、济南、苏州。根据当地统计局或搜房网统计，在这些城市中，北京、上海、深圳、杭州等地2009年9月普通二居室月租均价都在2000元以上，其中，北京2562元、上海2555元、深圳2500元、杭州2200元。而这些城市2008届大学本科生毕业半年后的月收入分别为2697元、2982元、2960元、2468元，全国本科生毕业半年后平均月收入为2133元，高职高专毕业生的收入更低。<br />
<br />
　　在这些大城市，一套普通住宅二居室的月租，基本相当于当地本科生毕业半年后的平均月收入。<br />
<br />
　　而根据廉思“蚁族”课题组的调查结果，生活在聚居村里的这上百万大学毕业生，收入从500到5000元不等，50%为1000―2000元，平均月收入为1956元，月均花销1676元。43%的人能略有结余，17%的人收不抵支，而只有10%的人结余较多。<br />
<br />
　　事实上，一线城市的地方财力和承受程度都有限度，一些发展中的二线三线城市拥有较高的收入，居住成本则相对较低。以苏州为例，当地普通二居室月租1456元，本科生毕业半年后平均月收入为2221元，二居室房租基本为月收入的2／3，相比北京、上海等地低廉。<br />
<br />
　　“外地一间房不如北京一张床”，这指的是大城市居住成本高企的问题。但另一个现实是大学毕业生“宁要北京一张床，不要外地一间房”的就业现状。“老爸认为，我在北京工作，就是北京人。”廉思告诉本报记者，曾经有一个“北漂”这样写道。<br />
<br />
　　这种事例数不胜数。小秦辞去了河北石家庄国企文职的工作，到北京一家出版公司，住了两个月的地下室；小汪辞去了安徽省政府机关公务员的工作，到北京陷入“半失业”。他们都对本报记者说：为了兴趣，为了理想。<br />
<br />
　　根源在于户籍制度和住房保障问题<br />
<br />
　　“实际上，大学毕业生的住房问题，教育部门想管管不了，因为教育部门只管在校生；到人事局，人事局说，他们是‘北漂’，没有户口，我们管不了，我们只管有户口的；劳动保障部门也管不了，好多人没有签劳动协议，大部分都是临时工，或者卖个电脑什么的，哪里有什么‘三险’、‘劳动协议’；城建部门也要北京户口才能有房子，所以也管不了。”调查过北京的大学生聚居村以后，廉思在接受本报记者采访时感慨说。<br />
<br />
　　廉思等人在北京的调查发现，大学毕业生聚居村中，超过85%的人为外地户口，仅15%的人是北京本地户口。<br />
<br />
　　住房和城乡建设部政策研究中心副主任王珏林接受本报记者采访时认为，能在市场上买得起房的仅有20%多，“更别提大学毕业生了”，住房保障明显不足。但针对大学毕业生的住房问题，他认为政府发放住房补贴更可取。“廉租房和公租房都只是针对有本地户口的人群。”王珏林说，“解决了半天，还是解决有户口的，没有户口的，就是没娘的，没人管了。主要问题还是户籍制度。”<br />
<br />
　　“我们的工作、生活已经发生了很大的变化，但户籍制度没有什么变化，它滞后于我们市场经济的发展。其实现在不应该再有户籍了。”王珏林说，“在每个城市，不管是谁，只要是纳税人，都应该享受城市的住房待遇。我们在一个城市工作，那是在这个城市纳税啊！”<br />
<br />
　　因此，他认为，制度应该认定：“确实没有房住的、有就业单位的，就发放一点补助。”“最起码能留住人才啊！”<br />
<br />
　　国务院发展研究中心研究员丁宁宁同样表示，房地产开发只售不租，廉租房针对的对象太狭窄，是形成大学生住房困境的主要原因。“不管怎么讲，房地产政策都要进行调整了。”丁宁宁说。<br />
<br />
　　廉思对本报记者说，大城市大学毕业生住房困境，实际还指向了城乡二元结构以及高等教育、就业政策等一系列问题。<br />
<br />
　　大学扩招，自2003年以来，大学毕业生逐年增加，2004年280万，2005年338万，2007年485万，2008年599万，2009年650万。“他们也想过回老家县里面，可是又想，我回县里干嘛啊？我学的是国际金融、国际贸易，我回县里做什么去啊？”他说，“我们的基层需要人。但是我们的村官制度、西部志愿者制度、大学生下基层制度，有没有配套的东西？大学生到农村里融入不进去，怎么样更好地让他们融入进去，真正地俯下身去做点事情？是不是就只是混个考研加分？”</div>

							
							
							
							
