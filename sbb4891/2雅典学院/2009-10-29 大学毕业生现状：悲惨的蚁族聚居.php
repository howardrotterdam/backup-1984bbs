　　唐家岭是海淀区最靠边的一个村子，从这里看不出任何京都的气息，是典型的城乡结合部。由于环城带地区交通便捷，生活成本低廉，可开发利用的土地相对较多，开发建设速度加快，就业、创业机会亦相对较多，加之这个地区大量合法和违法建设的出租房屋，使刚刚毕业的大学生在此落脚成为可能，因而形成聚居。在这里居住的大都是刚刚从学校毕业的大学生，有些甚至还是重点大学的毕业生，年龄大多和我相仿。他们有基本一样的情况，拿着一千多元左右的工资，租着每月三百元左右的床位，每天吃两顿饭，到工作单位要坐两个小时以上的公交车。唐家岭居住着四五万人，除了本村居民三千余人外，其他的都是如此生活。这些大学毕业生怀揣着梦想到学校，充满着理想出校门，但残酷的现实教会了他们什么是生活，小小的&quot;聚居村&quot;折射出一幅中国大多数年轻人生存现状的结构图。<br />
<br />
　　从唐家岭回来后，这些同龄人生活的场景时常在我眼前闪现，使我久久不能释怀。我决心要深入研究这个群体，我要了解他们的所思、所想、所为。我个人没有能力帮助他们解决窘境，但我可以通过自己的笔，让社会上更多的人了解他们、关心他们、帮助他们。有了这样的想法，我开始着手对这一群体展开研究。<br />
二 第一次调研<br />
<br />
　　对于这样一个新的生态群体，在没有前人研究可供参考的情况下，只有自己组织社会调查，才能获得有关这一群体的第一手资料。幸好我于2005-2006年曾挂职湖北省广水市市长助理，挂职期间我组织了针对广水市的经济社会调查，这段经历为我组织社会调研积累了丰富的经验。<br />
<br />
　　正在我埋头设计问卷、确定调研地点之际，北京市委领导找到我，委托我就北京市大学毕业生&quot;聚居村&quot;问题进行专项调查，这个想法与我不谋而合。有了政府的支持，我的研究工作得以迅速开展。<br />
<br />
　　调研问卷定稿、抽样地点选择、调研礼物筹备等前期准备工作完成后，已是2008年1月。由于临近寒假，大部分学生已经回家过年，调研人员不足成为我面临的首要问题。无奈之下，我回到中国人民大学，利用自己曾在校研究生会做过主席的便利，招募了一些不准备回家过年的研究生参与调研，这批学生里就包括后来一直和我做此项研究的徐启元。<br />
<br />
　　调研的过程并不顺利，2008春节恰逢我国遭受50年一遇的雪灾，受冷空气影响，北京也异常寒冷。我带着调研员入村调查，常常是问卷还没有做完，笔已经冻得写不出字来。一个多星期的调研，疲惫但有收获，刺骨的寒风和&quot;聚居村&quot;村民的热情形成了鲜明对比，由于年龄接近，我们和受访者之间有一种天然的亲切感。很多时候，常常是问卷还未做完，调研员和受访者就成了朋友。有些受访者不仅自己接受调查，而且还将其亲戚、老乡、朋友也介绍给调研员，这在很大程度上减少了我们调研的阻力。<br />
<br />
　　随着调查的深入，这一群体的面貌由模糊到清晰，逐渐显现出来：他们人数众多，大多聚居于城乡结合部或近郊农村，已经形成了一个个自然村落--&quot;聚居村&quot;。居住在&quot;聚居村&quot;生活压力很小，也更容易交到各种各样的朋友。于是，一批批大学毕业生住了进去，几年后，当他们有了固定的事业发展方向和足够的生活资料时，又纷纷搬了出来。他们有的毕业于名牌高校，更多的来自地方院校和民办高校；有的完全处于失业状态，更多的从事保险推销、餐饮服务、广告营销、电子器材销售等低收入工作。他们生活条件差，缺乏社会保障，民主权利缺失，普遍对社会公平存有疑虑，思想情绪波动较大，挫折感、焦虑感等心理问题较为严重，且普遍不愿意与家人说明真实境况，与外界的交往主要靠互联网并以此宣泄情绪。热情、开朗、迷茫、彷徨，形成了我们对&quot;聚居村&quot;村民第一次亲密接触的印象。<br />
<br />
　　调研结束后，我根据调查所得数据和北京市委提供的统计资料，写成了4万余字的研究报告。在这份调研报告中，我就&quot;聚居村&quot;的流动人口、出租房屋、基层管理力量配备、公共资源配置、治安状况和人居环境等情况进行了统计和分析。通过这份报告，基本上可以了解当前北京市大学毕业生&quot;聚居村&quot;的现状、突出问题和形成原因。报告还在北京已有流动人口管理工作的基础上，着眼于促进社会主义和谐社会首善之区建设，就如何进一步加强和改进大学毕业生&quot;聚居村&quot;管理服务工作提出了若干意见和建议。2008年4月，报告被北京市委提交中央，得到国务院总理温家宝、国务委员刘延东等中央领导同志的高度重视。我的研究获得了肯定，辛勤的努力换来了初步的成果，这更坚定了我要把这项研究继续深入下去的决心。<br />
三 80后<br />
<br />
　　由于&quot;80后&quot;所经历的特殊历史背景，他们在人们眼中一直没有很好的印象，早些年网络等媒体上到处是&quot;歧视&quot;&quot;80后&quot;的现象，包含攻击的字眼处处可寻，如被称&quot;垮掉的一代&quot;、&quot;最没责任心的一代&quot;、&quot;愚昧的一代&quot;、&quot;最自私的一代&quot;、&quot;最叛逆的一代&quot;。然而2008年，让很多人都扭转了对&quot;80后&quot;的看法，原来认为是半吸毒状态沉迷于网络的&quot;80后&quot;忽然高尚了一把：汶川地震的奋勇果断、行动魄力以及奥运会的热情大方、责任意识。至此，各种媒体对&quot;80后&quot;进行的积极报道和正面宣传开始进入主流趋势。<br />
<br />
　　同一代人得到了截然相反的评价。中国的&quot;80后&quot;究竟是令人骄傲的&quot;鸟巢一代&quot;，还是令人担忧的&quot;迷茫一代&quot;呢？由此，我们不得不思考这样一个问题：究竟该如何认识&quot;80后&quot;这个复杂的群体。<br />
<br />
　　&quot;聚居村&quot;中居住的&quot;蚁族&quot;，大概能为我们理解&quot;80后&quot;提供一个注脚。&quot;蚁族&quot;大都出生于上个世纪80年代，是一个典型的&quot;80后&quot;群体。他们是当今社会中&quot;最为敏感的人群&quot;：充满活力，满怀理想，或者雄心勃勃，具有挑战意识。大学教育给了他们知识和涵养，也曾许给他们美好的未来。然而，工作后的境遇，&quot;聚居村&quot;的生活，青春梦想无处寄托，使他们不得不学会忍受焦虑且不具确定性的暗淡时光。但同时，他们乐观、开朗，相信自己的选择，情愿从底层做起，在慢慢的积累和沉潜中，期待着未来的爆发。<br />
<br />
　　透过&quot;蚁族&quot;，读者对&quot;80后&quot;能得出自己的评价。但可以肯定的是，&quot;蚁族&quot;给我们揭示的是一个真实的&quot;80后&quot;，在&quot;蚁族&quot;的身上既有&quot;鸟巢一代&quot;的光鲜，也有&quot;迷茫一代&quot;的阴影。<br />
<br />
　　与&quot;90后&quot;不同，&quot;80后&quot;存有从小到大眼见祖国由相对困难直至今天成就的完整记忆，度过了没有高科技围绕、没多少物质享受却简单充实的童年时代，以及率先跨入中国信息时代接触新生事物、成为新千年第一批彰显个性族群的少年时代，最终成为现今开始思考民族荣誉国家前途、日渐成熟进入社会有所担当的青年一代。因此，&quot;蚁族&quot;作为改革开放后出生的一代，目睹着中国经济的快速增长和社会的深度转型，作为世界新经济体中的社会结构成长要素，他们所经历的国家巨大变革，以及在他们身上所表现出来的价值观念、行为方式和生活意识的变化，折射出中国社会特定转型时期的社会历史变化。<br />
但是，作为&quot;80后&quot;的&quot;蚁族&quot;同样孕育着危机。中国传统社会2000年来能够保持相对稳定非常重要的方面是有一个比较通畅的下层社会向上层社会的流动机制，即自隋唐开创的科举制。每年，数以万计的莘莘学子通过这条路改变了自己的命运。科举制给所有读书人以通过科考而非钱财、出身、关系、背景改变自己命运（学而优则仕）的通道。但当今时代，在这权力和人情社会，越是&quot;苦读&quot;的平民家庭出身的孩子，机会可能越少。许多靠近权力的机关和垄断行业里，越来越没有平民和贫苦人家孩子的缘分。父辈的权力和&quot;人脉&quot;，会以某种方式&quot;世袭&quot;。这种情况在&quot;蚁族&quot;身上体现得淋漓尽致：根据我们的调查显示，&quot;蚁族&quot;大多来自农村和县级市，家庭收入较低，他们的父母处于社会中下层。对于&quot;蚁族&quot;成员来说，他们从小被灌输的是好好学习，将来考取大学，从而改变自己的人生。十年寒窗考上大学后，他们仍然为了今后能找到一个好工作而努力学习。但毕业时他们却发现，由于自己没有&quot;硬&quot;关系，只能又回到&quot;村&quot;里。当他们梦想破灭的那一刻，他们会将人生中的种种不如意，归因于整个社会，从而使自己和社会对立起来。可以想象，长此以往，必将引发严重的社会问题。<br />
<br />
　　应该承认，青年群体蕴含着极大的社会能量。这种社会能量既可以成为伟大的创造力量，也可以成为巨大的破坏力量。上世纪六十年代欧洲大规模的学生运动中，我们可以看到这样的横幅&quot;破坏的欲望就是创造的欲望&quot;--这是无政府主义鼻祖巴枯宁的名言。青年这种社会能量的释放方向往往在于引导，包括进行政治的、经济的、民族的、情感的引导。青年是社会的希望，也是社会的晴雨表，代表社会最敏感的触觉。在法国、德国和拉美诸国，种种反体制、反政府甚至反社会的极端意识形态大多发源于青年群体，或者经由他们向社会其他群体扩散。尤其是青年群体可以熟练地通过互联网等新型媒介进行信息沟通与活动，这对于社会舆论乃至意识形态安全都具有较大影响。任何一种青年的群体行为都具有超前性（包括理想主义的幼稚）、超越性（现实和自我）和社会批判性（包括非理性的攻击倾向），这是青年群体行为独有的特点。而&quot;蚁族&quot;是青年中受过高等教育，能够明确表达自己社会诉求、主体意识最强的活跃群体。如果这样一群&quot;精英候选人&quot;无法参与社会，或者社会使他们&quot;走投无路&quot;，让他们以年轻脆弱的心灵和一无所有的经济能力裸露于经济危机之下，必将对我国社会的和谐与稳定构成潜在威胁。因此，如何更好地了解&quot;蚁族&quot;的形成和发展，掌握他们的群体特征和生存状况，倾听他们的呼声和意见，让他们更好地、更快地融入社会，成为社会的有机组成部分，并且得到广泛的认同和尊重，是一项紧急的、艰巨的且长期的工作，需要社会各界共同做出努力，付诸实践。<br />
三十年前的1980年，《中国青年》在第5期上刊发了一篇署名潘晓的读者来信《人生的路呵，怎么越走越窄》，这篇并不算长的文章开头便是一种绝望的口气：&quot;我今年23岁，应该说才刚刚走向生活，可人生的一切奥秘和吸引力对我已不复存在，我似乎已走到了人生的尽头。&quot;然后，&quot;潘晓&quot;历数了自己在家庭、单位、朋友、恋人面前所遭受的挫折，由此潘晓得出一个结论：&quot;任何人，不管是生存还是创造，都是主观为自我，客观为别人……只要每一个人都尽量去提高自我存在的价值，那么整个人类社会的向前发展也就成为必然了。&quot;<br />
<br />
　　但潘晓还是感到困惑：&quot;有人说，时代在前进，可我触不到它有力的臂膀；也有人说，世上有一种宽广的、伟大的事业，可我不知道它在哪里。人生的路呵，怎么越走越窄？&quot;<br />
<br />
　　潘晓的困惑也正是当时很多青年的困惑。很快，潘晓的来信引起了无数年轻人的共鸣。《中国青年》持续发表讨论文章，共收到读者来信6万多封，有数千万人参与了这场有关社会伦理与人生观的大讨论。这是一场中国历史上为数不多的全民性大讨论，是改革开放后中国青年接受的第一场思想洗礼。<br />
<br />
　　如今的&quot;蚁族&quot;使我不由得想起上世纪80年代初的潘晓大讨论，巧合的是，和我接触的许多&quot;蚁族&quot;，虽表述有所不同，但都不约而同地发出&quot;人生的路为何越走越窄&quot;的呼号。上个世纪潘晓事件引发人生观、价值观大讨论时，&quot;蚁族&quot;的大部分还未出生。两个世纪，同一种声音，个中的含义值得我们每一个人去品味、去思考……<br />
蚁族&quot;全解<br />
<br />
　　本部分将对&quot;蚁族&quot;的基本概念及产生原因进行介绍，并结合课题组的研究成果，对&quot;蚁族&quot;的心理状况、性-爱情-婚姻、收入状况、职业状况、教育状况、网络行为以及集体行动倾向等方面进行简单分析。需要说明的是，由于篇幅所限，本章省略了复杂的模型推导，而直接用通俗易懂的语言给出了研究结论。当然，有兴趣的读者，可以从课题组另行出版的学术专著中了解到这些结论的详细演算过程。<br />
<br />
　　一 基本概念<br />
<br />
　　1．大学毕业生低收入聚居群体<br />
<br />
　　从名称可以看出，该群体具有三个典型特征：大学毕业，低收入，聚居。<br />
<br />
　　该群体是大学毕业生群体，即该群体成员均接受过高等教育。这就限定了群体的界限，即没有受过高等教育的青年农民工以及务农青年不属于此群体的范围之内。此外，根据课题组研究显示：该群体年龄主要集中在22-29岁之间，以毕业5年内的大学生为主，&quot;80后&quot;占到调查总数的95.3%。也就是说，该群体以&quot;80后&quot;的大学毕业生为主，是一个&quot;80后&quot;高知群体。<br />
<br />
　　该群体是低收入群体，群体中大多数人从事简单的技术类和服务类工作，以保险推销、电子器材销售、广告营销、餐饮服务为主。群体中甚至有的完全处于失业状态，全靠家里接济度日。根据课题组研究显示，该群体月均收入为1956元，既大大低于城镇职工平均工资，也低于大学毕业生毕业半年后的平均工资，因而可将其定位为低收入群体。因此，该群体是大学毕业生中月均收入2000元左右的低收入群体。<br />
<br />
　　该群体呈现出聚居的生活状态。根据课题组研究显示，该群体主要聚居于人均月租金377元，人均居住面积不足10平方米的城乡结合部或近郊农村，已经形成了一个个聚居区域--&quot;聚居村&quot;。<br />
<br />
　　这一群体的人数到底有多少，没有确切的统计数字。但据课题组研究表明，仅北京地区保守估计就有10万以上。此外，上海、武汉、广州、西安等大城市也都大规模存在这一群体。<br />
<br />
　　2．蚁族<br />
<br />
　　&quot;大学毕业生低收入聚居群体&quot;，名称虽然一目了然，但文字过长，不便于记忆。因此，我们采用&quot;蚁族&quot;来命名该群体，不仅指代简单准确，而且抓住了这一群体的本质特征。<br />
<br />
　　首先，蚂蚁具有较高的智商。据相关研究表明，蚂蚁有25万个脑细胞，在所有的昆虫中，是最聪明的物种。蚂蚁的高智商能用来描绘该群体所具有的&quot;高知&quot;、&quot;受过高等教育&quot;等特点。其次，蚂蚁属群居动物，一个蚁穴里常常有成千上万只蚂蚁，这也与该群体在物理状态下呈现出聚居生活的特征相吻合。此外，蚂蚁很弱小，但若不给予其足够的重视，蚂蚁也会造成严重的灾害（如蚁灾），因此有人称蚂蚁为&quot;弱小的强者&quot;。蚂蚁这些特点与该群体弱势、低收入、不被人关注，易引发诸多社会问题等方面极为相似。<br />
<br />
　　此外，蚂蚁还有许多容易被我们忽视的优点，而恰恰是这些优点，与这个群体有着高度的相似性。比如蚂蚁永不言弃的精神：如果我们试图挡住一只蚂蚁的去路，它会立刻寻找另一条路，要么翻过或钻过障碍物，要么绕道而行。还比如蚂蚁所具有的期待情怀：整个冬天蚂蚁都憧憬着夏天。在严冬中，蚂蚁们时刻提醒自己严寒就要过去了，温暖舒适的日子很快就会到来。即便是少有的冬日暖阳也会吸引蚂蚁们倾巢而出，在阳光下活动活动筋骨。一旦寒流袭来，它们立刻躲回温暖的巢穴等待下一个艳阳天的召唤。此外蚂蚁还有勤勤恳恳、全力以赴的工作态度等。这些特点，都是&quot;大学毕业生低收入聚居群体&quot;的真实写照。<br />
<br />
　　综合以上因素，我们将这个群体定名为&quot;蚁族&quot;，并根据该群体所处地域的不同，分别冠之以京蚁（北京）、沪蚁（上海）、江蚁（武汉）、秦蚁（西安）、穗蚁（广州）等称呼。<br />
3．大学毕业生&quot;聚居村&quot;<br />
<br />
　　大学毕业生&quot;聚居村&quot;，又称为&quot;蚁域&quot;。它并不是实际意义上的自然村行政单位，而是指一定行政辖区范围内出租屋相对集中，且房屋出租对象主要为刚毕业大学生的聚居区域。&quot;聚居村&quot;住宿条件简陋，已经形成了自给自足、自我封闭的低层级衍生经济圈。无照经营的小餐馆、小发廊、小作坊、小诊所、小网吧、成人用品店和低档娱乐场所等各类经营门店大量集中且无序增加，入室盗窃、抢劫斗殴等刑事案件频发，生活状态堪忧。<br />
<br />
　　大学毕业生&quot;聚居村&quot;是一个非常复杂的社会问题，也是城市管理中不可回避的一个难点问题，小小的&quot;聚居村&quot;折射出一幅中国大多数年轻人生存现状的结构图。<br />
<br />
　　4．校漂族<br />
<br />
　　&quot;校漂族&quot;是指那些已经毕业但为了考研或找到更加理想的工作（包括留在大城市）或害怕面对激烈的就业环境而继续留在母校或附近其他学校周围的大学毕业生群体。他们住在学校及其周围，在食堂就餐，到图书馆学习，去操场踢球，在学校上网，还经常到学校听课和各种讲座、参加考研培训以及学校举办的人才招聘活动等。学校的公共场所里几乎都可以看到他们的身影，他们俨然是高校中的一员，而事实上已经走上社会，与学校并无隶属关系，因此被称为&quot;校漂族&quot;。<br />
<br />
　　&quot;校漂族&quot;与&quot;蚁族&quot;有着本质的不同。首先，&quot;校漂族&quot;是以考研毕业生为主，日常消费多由父母供给；而&quot;蚁族&quot;则多以从事低收入工作为主，即便是考研，也大多是边工作边学习。其次，&quot;校漂族&quot;多散布于学校内和校园周边，居住方式很少呈现聚居形态；而&quot;蚁族&quot;则位于城乡结合部，一般远离高校，但交通便利，居住方式呈现出&quot;聚居村&quot;状态。此外，&quot;校漂族&quot;最早出现在上世纪80年代末的大学毕业生&quot;国家分配与双向选择&quot;并行之时，此后便连年存在；而&quot;蚁族&quot;则是近年来在高校扩招和大学生就业难等大背景下，大城市中新近出现的身份地位群体。<br />
<br />
　　可见，&quot;校漂族&quot;和&quot;蚁族&quot;具有不同的行为特征和产生原因，不能作为同一个群体来加以描述和研究。<br />
二 产生原因<br />
<br />
　　&quot;蚁族&quot;现象的产生，既有国家就业形势严峻的原因，也有我国户籍制度的因素；既有&quot;聚居村&quot;本身个性的问题，也有城乡结合部共性的问题；既有大学毕业生本身个性的问题，也有流动人口共性的问题。<br />
<br />
　　1．宏观原因<br />
<br />
　　（1）大城市的吸引力。根据社会学家芒福德的观点，大城市就像是一个磁体，吸引着数以万计的人才和各种资源汇集到一起，特别是大城市经济社会不断发展，城市的魅力和吸引力也在不断增强。目前，包括首都北京在内的我国一些大中城市，其经济活力和生活水平已经达到了相当的水平，与一些小城镇或西部的城市相比，可以为大学生提供更为优厚的待遇和更好的发展空间，毕业生当然会尽可能地选择留在大城市或者到沿海地区就业。课题组研究发现，在北京、上海、广州、武汉、西安等大城市中均有大规模的&quot;蚁族&quot;存在。另外，由于我国户籍制度的客观影响，大部分来自农村的大学生都倾向于留在城市工作，这无疑更加剧了大城市的磁体效应。以北京为例，北京成功举办奥运会后，在其巨大的吸引力感召下，京内外的大学毕业生普遍认为在北京会获得更多的发展机会，而去外地工作则会增加发展风险，因此更愿意在北京工作。<br />
<br />
　　（2）我国就业形势的变化。2003年初，当时我国首批扩招大学生进入社会，与下岗再就业职工和民工潮汇聚成就业洪峰，造成我国就业压力空前增大。此后，我国大学毕业生人数便连年增加，2004年为280万，2005年为338万，2007年为485万，到2008年达到599万人。据中国社科院《社会蓝皮书》统计，2009年大学毕业生的人数将在2008年599万的基础上再增加50万左右，达到650万人。而与此同时，我国社会正经历城市化、人口结构转变、劳动力市场转型、高等教育体制改革等一系列结构性因素的变化。在这些因素的综合作用下，在我国的城市特别是大城市中，必然出现大学毕业生滞留现象。<br />
<br />
　　（3）我国就业政策的调整。2002年3月，教育部、人事部、公安部、劳动保障部共同制定了《关于进一步深化普通高等学校毕业生就业制度有关问题的意见》，其中规定:未落实工作单位的大学毕业生，学校可根据本人意愿，其户口两年内可继续保留在原就读的高校，待落实工作单位后，将户口迁至工作单位所在地。一方面骤然严峻的就业形势，使有一部分学生不得不待在大城市寻找考研深造或其他就业机会，另一方面国家就业政策，也为一部分人继续留在大城市提供了政策支持。<br />
<br />
　　（4）大学生择业观的相对滞后。1998年开始的大学扩招，使我国大学入学率在短短七八年内达到了21%，到2020年，我国将实现高等教育毛入学率40％的目标，按照国际标准已进入高等教育大众化时代。因此这也必然要求大学毕业生的就业从精英化走向大众化，让更多受过高等教育的人进入普通劳动者的行列。针对这种情况，2005年7月，中共中央办公厅、国务院办公厅印发了《关于引导和鼓励大学毕业生面向基层就业的意见》(中办发[2005]18号)，要求地方党委、各级政府部门进一步完善毕业生就业政策和服务体系，形成&quot;党委统一领导、政府统筹协调、部门全力支持、社会共同努力&quot;的毕业生就业工作新体制，建立责任明确的毕业生就业工作领导与运行机制，采取各项措施扫除毕业生就业的政策性和体制性障碍，努力营造良好的就业环境和创业环境，使大学毕业生尽快地找到适合的就业岗位，或者尽快地走上自主创业的道路。但是很多学生在思想上还没有完全适应这种转变，越是人才饱和的发达地区或大城市，前去择业的就越多，反之，越是急需人才欠发达的中西部地区或农村越难招到人才。&quot;宁要北京一张床，不要外地一套房&quot;的情况仍然普遍存在，落后地区出来的大学生宁可在大城市里做&quot;漂一族&quot;，也不愿回到自己的家乡，或者到欠发达地区工作。<br />
（5）高等教育发展与社会需求的差异。大学生就业过程中存在很多体制性阻碍，也是导致&quot;蚁族&quot;出现的原因之一。近年来，社会对大学生需求的增长速度并没有赶上大学毕业生增加的速度，而对能够作为初级工人、高中和职专毕业生的需求却有增无减。这说明，伴随着高等教育大众化，大量接受了大学教育而没有一技之长的大学生面临着就业尴尬局面。此外，高等学校专业设置因致力求全而有部分雷同。大学毕业生就业已经社会化，实行自主择业，但市场需求情况并没有及时成为专业设置、招生人数划定的风向标。招生部门只按照办学设施和师资力量等基本条件负责招生，这就造成了学校专业设置与市场需求错位，专业供需矛盾突出，导致大量大学生就业出现困难。因此，高校教育体制机制与社会需求的差异，是形成&quot;蚁族&quot;的潜在原因。<br />
<br />
　　2．微观原因<br />
<br />
　　（1）房租低廉，交通便利是&quot;蚁族&quot;形成的客观原因。随着中心城区和近郊区各项管理措施逐步到位，流动人口必然向周边环城带地区迁移。同时由于环城带地区交通便捷，生活成本低廉，可开发利用土地相对较多，开发建设速度加快，就业、创业机会亦相对较多，加之这些地区大量合法和违法建设的出租房屋使刚刚毕业的大学生在此落脚成为可能，势必在此形成聚居。课题组在&quot;蚁族&quot;聚居较多的昌平区沙河镇调查了解到，该村农业转制基地于2006年10月建设完毕，盖有大量房屋，其主要建筑面积20平方米，一室带厨房、厕所，建成后便在互联网上发布出租房信息，房租为200元左右/月。由于其房租费用较低，交通离市区较近，相对便利，居住条件适合部分大学毕业生所需，于是便有部分大学毕业生从北京各地前来租住，产生了&quot;聚居村&quot;。<br />
<br />
　　（2）追求群体间的认同是&quot;蚁族&quot;形成的主观原因。大学毕业生刚刚步入社会，熟悉的人群能给其以较大的安全感。因此，他们往往在毕业前夕和师兄师姐联系，希望与他们居住在同一区域，久而久之，就形成了&quot;聚居村&quot;。这时的&quot;聚居村&quot;就像一个相对熟悉的港湾，毕业生那疲惫的航船要在港湾中抛锚、躲避。不论港湾同意接纳与否，也不管港湾是否能够承受，寻求庇护的航船扎堆似的往里挤。课题组在海淀区唐家岭&quot;聚居村&quot;调研时发现，许多&quot;蚁族&quot;成员都在&quot;聚居村&quot;内形成了自己的生活圈和交往圈，圈子里的人大都是自己的同学或老乡。<br />
<br />
　　（3）对独立生活状态的追求，导致了少量在校生选择在&quot;聚居村&quot;居住。其中，有些在校大学生是出于性格缺陷而在&quot;聚居村&quot;居住。这部分学生或者不善于处理同学关系，或者不习惯集体生活。选择在校外租房居住，既清静又省事。由于高校学生来自不同地区，室友们在生活习惯、卫生习惯等方面都存在一些差异，因而在集体生活中往往难以相融，甚至产生矛盾或纠纷。于是一部分同学便怀着&quot;惹不起、躲得起&quot;的心态避而远之，在校外营造一个属于自己的生活天地和自由空间。<br />
<br />
　　还有些在校大学生是与恋人同居，营造&quot;二人世界&quot;而选择在&quot;聚居村&quot;居住。目前在校大学生谈恋爱的较多，而且大学生中同居现象较为普遍。毕竟学校有自己的规章制度，集体宿舍显然不允许，也不适合这些同学&quot;过自己理想的生活&quot;。为了有更多单独相处的时间，在校外租房居住成为他们最好的选择。<br />
<br />
　　此外，还有部分学生是为了寻找学习环境或为就业需要而在&quot;聚居村&quot;居住。由于民办大学是以自考为主的高等教育机构，一些想拿文凭的学生，为抓紧时间学习，提高学习效率，觉得集体宿舍满足不了自己的需求，只好自己寻找住房。许多民办大学毕业生未找到工作，为了就业需要滞留在&quot;聚居村&quot;，主要在于交通方便，便于在京找工作；再加上房租便宜，所以居住在此。<br />
三 心理状况<br />
<br />
　　课题组对&quot;蚁族&quot;的心理状况进行了研究，运用国际通用症状自评量表（SCL-90）、消极完美主义量表、满意度量表、应对方式量表、社会政治控制量表等，对该群体心理健康状况、完美主义人格、社会政治控制、社会支持、生活满意度和应对方式等六方面进行了分析，得出以下结论：<br />
<br />
　　1．课题组对SCL-90所包括的9个因子：躯体化、强迫、人际关系敏感、抑郁、焦虑、敌对、恐怖、偏执、精神病性等因子分平均值进行了统计分析，从高到低依次为；强迫均分2.05、人际关系敏感均分1.82、抑郁均分1.75、敌对均分1.74、偏执均分1.74、焦虑均分1.72、精神病性均分1.66、躯体化均分1.46和恐怖均分1.45。分值呈现的状况表明，&quot;蚁族&quot;目前正处于身体状态最好的年龄段、自我效能感较强，导致躯体化、恐怖等分值较低。同时，&quot;蚁族&quot;正处于从校园人到社会人的转变期，社会支持与生活事件对&quot;蚁族&quot;的影响相对较大，导致强迫、人际关系敏感、敌对和焦虑症状等均分相对较高。 <br />
<br />
　　将上述分值与全国成人常模比较后发现,&quot;蚁族&quot;在强迫、抑郁、敌对、人际关系敏感、焦虑、偏执、精神病性等因子上均分高于正常成人值，显示&quot;蚁族&quot;心理状况呈现亚健康水平，心理问题不容乐观。<br />
<br />
　　2．从整体心理健康状况来看，&quot;蚁族&quot;正处于从校园人到社会人的转变期，社会支持与生活事件对该群体的影响相对较大。&quot;蚁族&quot;的工作非常不稳定，许多人都在不停地换工作以追求更好的职位。工作的频繁更换使他们长久保持在一种高唤醒水平状态，对各种学习信息及招聘信息的高需求导致他们对信息非常敏感，从而使他们有规律、强迫地关注某些问题，这可能成为强迫因子分较高的原因之一。同时，他们的收入相对较低，各种社会福利和保障都相对缺乏，这样的状况会造成高焦虑且相对比较敏感的现状。此外，&quot;蚁族&quot;敌对因子得分也偏高，可能是以下原因所致：（1）&quot;蚁族&quot;刚踏入社会，感觉到社会的现实与自己书本中、想象中不相符，难免就会对社会不满，对各种政策各种社会事实带着潜意识的抗拒；（2）&quot;蚁族&quot;大学时是&quot;天之骄子&quot;，但随着高校招生规模逐年扩大，高等教育由精英教育向大众化教育的转变，&quot;蚁族&quot;的失落感骤然增强，这种失落感会令&quot;蚁族&quot;的认知和情感趋向负面。<br />
<br />
3．&quot;蚁族&quot;完美主义人格倾向显著高于全国平均水平，自我期望较高。具体表现在以下几个方面：期望通过自己的努力获得极高的成就；所定下的目标远远高于周围人群；比较倾向于理想主义。&quot;蚁族&quot;主要是毕业五年内的大学生，刚接触社会，处于为梦想奋斗的初期。尽管&quot;聚居村&quot;的条件艰苦，但他们拥有着较高的目标和期望，仍然愿意留在这里为自己的理想和未来拼搏。这样的心理状态和人格倾向，使得个体在奋斗过程中当期望与现实产生巨大差异时，在心理层面极易产生相对剥夺感，而这种剥夺感会影响到个体的身心健康状况，比如出现焦虑、抑郁情绪或敌对感等。<br />
<br />
　　在目前的社会现状和背景下，&quot;蚁族&quot;很难在短期内实现他们的理想和愿望，他们会在持续的挫折感中深刻地体会到这一点。而&quot;蚁族&quot;聚居生活的状态，很可能会使这样的认识和情绪广泛传播，形成群体认同。如果没有合理的宣泄途径，将会对该群体自身和社会造成负面影响。<br />
<br />
　　4．&quot;蚁族&quot;的个人社会政治控制感高于一般大众水平，即&quot;蚁族&quot;的社会政治态度比一般大众激进或偏激，这与该群体认为自身政治地位较低和对自身民主权利不满意有密切关联。此外，近几年来，我国大学毕业生面临巨大的就业压力，不稳定的工作状况导致&quot;蚁族&quot;生活窘迫，人生发展的各个方面都与预期相差较远，这使得他们感受到较为强烈的挫折感，因而倾向于拥有较高的&quot;对社会事件和环境的影响力和控制力&quot;。<br />
<br />
　　5．&quot;蚁族&quot;社会支持的状况不理想，得到的社会支持极少，甚至低于全国10个城市的平均水平，&quot;蚁族&quot;中仅一半人对自己人际交往状态表示满意。在与&quot;蚁族&quot;交往最亲密的人中，朋友占70.3%，亲人占24.62%；他们与朋友日常联络使用最多的方式是电话，说明最亲密的朋友可能并不在身边，就近的交往状态可能广却不深，因此总的社会支持状况低于一般水平。<br />
<br />
　　此外，&quot;蚁族&quot;中女性成员的社会支持状况相对优于男性。女性相较于男性，更多使用了自身主、客观社会支持源。<br />
<br />
　　6．在生活满意度方面，&quot;蚁族&quot;的状况不容乐观。&quot;蚁族&quot;的婚姻状况、性生活状况以及家庭经济收入是影响其生活满意度的主要因素。&quot;蚁族&quot;的生活满意度显现出以下几个特征：<br />
<br />
　　对经济收入最不满意，对人际交往最满意。&quot;蚁族&quot;抱怨最多的是经济收入、社会福利，职业声望等，与此同时，他们对人际交往的满意度较高，对经济收入最不满意主要在于该群体对收入的预期较高，有高达74.8%的受访者认为自己目前的收入与其预期不相符。<br />
<br />
　　对生活的预期乐观，但对政治地位和民主权利不抱以信心。79.2%的受访者都乐观地认为未来三年内的总体生活状况会有所改善。但是对政治地位和民主权利这两方面，大多数受访者（政治地位：55.9%，民主权利：51.3%）则不抱以乐观预期，认为不会有多大改善。<br />
7．在应对方式上，&quot;蚁族&quot;选择最多的是网络曝光和媒体曝光，其次是&quot;上访&quot;、&quot;公开集会&quot;、&quot;联合亲友对抗&quot;、&quot;发牢骚&quot;和&quot;忍受&quot;，赞同度最低的是 &quot;罢工&quot;和&quot;游行示威&quot;。&quot;蚁族&quot;作为&quot;80后&quot;社会群体，其对网络的熟练程度是整个社会中最高的一个群体，其在利益表达时会自然而然地利用网络这一工具来进行，从网络上汲取信息然后经过个人加工后再反馈到网络空间中去。2008年网络舆论威力已然显现，作为&quot;草根力量&quot;登上舞台象征的&quot;烟照门&quot;、&quot;考察门&quot;、&quot;六个警察打死一个人&quot;以及2009年的&quot;躲猫猫&quot;事件等也给&quot;蚁族&quot;运用网络手段表达自身利益以很大的想象空间。<br />
<br />
　　赞成用&quot;上访&quot;和&quot;公开集会&quot;表达自身利益的方式仅次于&quot;网络曝光&quot;和&quot;媒体曝光&quot;。需要指出的是，&quot;蚁族&quot;对总体生活状况越不满意，则他们越不赞成用&quot;罢工&quot;的方式来表达自身利益。这可能是由于中国的特殊体制与西方不同，罢工运动在中国出现得很少，而且效果也不理想。<br />
<br />
　　同时研究也表明&quot;蚁族&quot;在应对方式方面无性别差异，他们的教育程度、家庭背景、成长环境等都对其采用何种应对方式没有显著影响。但该群体的人格、心理健康以及社会支持状况都与他们采取的应对方式显著相关。<br />
<br />
　　目前该群体的应对方式需要引起重视。应对方式是个体面临应激情景时为减少压力或伤害而做出的认知或行为努力，是个体面临挫折和压力时应激性的反应。现今&quot;蚁族&quot;正处于高就业压力和高期望值导致的挫败感之中，如果这种挫败感得不到适宜的调节或合理的归因，很容易导致该群体做出一些过激的行为，因此，应当重视调适&quot;蚁族&quot;的挫折心理，引导他们对目前的挫折进行合理归因，从而保证社会的安定和谐。<br />
<br />
　　综上所述，&quot;蚁族&quot;的总体心理健康水平低于全国平均水平，消极完美主义特质明显，大多数人对生活状态不满意，社会支持较低，社会政治控制力相对较高，且个体强烈地感受到一种心理层面的相对剥夺感。这与该群体目前面临的较高的生存压力，频繁更换工作或找不到自己理想工作的现状是密切相关的。尤其在金融危机的大背景下，严峻的就业形势及压力，理想与现实的差距，冲击着他们的内心。因此，应当对&quot;蚁族&quot;心理健康状况给予高度重视。<br />
四　性-爱情-婚姻<br />
<br />
　　&quot;性-爱情-婚姻&quot;作为一个实体存在系统，是人生的重要组成部分。课题组调查显示，&quot;蚁族&quot;中未婚人数占调查对象的93%，在未婚者中有49%没有恋人。与异性同居的人占到被调查对象的23%，但最近一个月内有性生活的人占到被调查对象的33%。从这些数据对比可以看出：&quot;蚁族&quot;结婚的比例小于与异性同居的比例，与异性同居的比例小于最近一个月有性生活的比例，最近一个月有性生活的比例小于有恋人的比例。<br />
<br />
　　1．婚姻状况与同居状况对比。未婚的人数占到被调查总体的93%，结婚的为7%，然而与异性同居的只有23%。这说明与异性同居的比例大大高于结婚的比例。与结婚相比，更多的人选择了不结婚同居生活。<br />
<br />
　　2．同居状况与近一个月内性生活状况对比。与异性同居的人占到被调查对象的23%，但最近一个月内有性生活的人占到被调查对象的33%，高出与异性同居比例的10%。如果假定同居的人都在最近一个月有过性生活，证明除了同居的人外，还有10%的人有过性生活。那么这一部分人可能是和未同居在一起的恋人之间发生的，也有可能是买性、一夜情等。<br />
<br />
　　3．&quot;蚁族&quot;存在&quot;婚恋困境&quot;。&quot;蚁族&quot;中，未婚人数占总数的比例特别高（93%），绝大部分处于未婚状态。在未婚人群中，几乎一半的人没有恋人。&quot;蚁族&quot;已经经历过大学阶段，有恋人的比例应该明显高于大学生群体，但与大学生群体进行对比分析后发现，该群体与大学生群体在此方面差距很小。&quot;蚁族&quot;的收入水平较低，社交圈子小，恋爱对象的选择范围小，整日忙于找工作或工作，谈恋爱的机会也少。如果经济条件允许，该群体的结婚比例应该比较高，但目前只有7%，结婚的比例很低。可见，&quot;蚁族&quot;绝大多数正处于生理旺盛期，他们渴望同异性交往，在恋人或夫妻方面得到感情上或生理上的支持和安慰，但由于没有稳定的收入来源，没有固定的住所，使他们无法在婚姻和恋爱的问题上考虑更多，所以大部分毕业生选择了单身或同居的方式来生活。<br />
<br />
　　4．&quot;蚁族&quot;的性需求难以得到满足。在93%未婚人群中有恋人的人占51%，其中的23%选择了同居的生活方式，33%的人在最近一个月内有过性生活。但大部分处于未婚、不与异性同居、最近一个月没有性生活的状况。如果假定最近一个月内有性生活的人的性生活对象为恋人，那么证明该群体中有恋人的人群之中将近半数的人没有性生活，更多的是感情上的交流和支持。就全国成年人而言，平均性生活频率是在每星期1次左右。然而该群体的67%的人最近一个月没有性生活。对比之下，&quot;蚁族&quot;在性需求方面难以得到满足，人际性活动的缺失很可能会通过独自性生活（如自慰）或其他途径进行宣泄，如果通过其他途径则在很大程度上支持了地下性产业的发展。 <br />
<br />
　　5．&quot;蚁族&quot;不同性别之间没有显著差异。课题组通过t检验发现，男性和女性在恋爱方面、婚姻方面、是否与异性同居方面、最近一个月内有没有性生活方面差异不显著。并且和总体的分析一样，无论男性还是女性都存在&quot;婚恋困境&quot;和性需求难以得到满足的问题。<br />
<br />
　　6．个人收入、居住面积和人际关系是影响有没有性生活的主要因素。&quot;婚姻&quot;作为政府的一种&quot;制度设置&quot;，实际上只有性关系才是普通人在日常生活中的&quot;生活实体&quot;。课题组运用Logistic回归统计方法进行分析，得出影响&quot;有没有性生活&quot;的因素为：个人收入、居住面积和人际关系。这三个方面也是有没有性生活的最基本的条件。收入、居住面积和人际关系为&quot;蚁族&quot;性生活提供了最起码的物质保障、地点和对象，这三者是最基本也是最显著的影响因素。课题组在调研中曾遇到在男性宿舍留宿女性的情况，一个十几平方米的宿舍里同时居住四个男人和一对情侣。虽然居住空间隔着帘子，但是性生活的质量以及对其他人造成的影响不容忽视。<br />
<br />
　　综上所述，&quot;性-爱情-婚姻&quot;的现状影响了&quot;蚁族&quot;对生活的满意度，性需求难以得到满足影响身心健康。即使对于有性生活的人而言，其性生活的质量也要受到性生活的地点和环境的影响。如果长期压抑，可能导致犯罪，影响社会的和谐与稳定。 <br />
