<br />
她说：“我昨天写了4个小时，我的手指头全都发黑了。”<br />
<br />
高耀洁这个月初从出版商处接到新书《中国爱滋病祸》的最后修订本，并已经从头到尾复核了这本30万字的着作，修订本上到处都看到她剔出来的一些错误，另外至少有100页的笔记。<br />
<br />
高耀洁的第一本着作《血灾：一万封信》，去年年底出版，内容重新整理了她5年前在大陆出版的书，补上了一些当年被当局禁止发表的资料。她已经完成了第三本着作《100宗传染病例》，有待香港方面出版。最后，她将修订两年前的自传《高耀洁的灵魂》。<br />
<br />
