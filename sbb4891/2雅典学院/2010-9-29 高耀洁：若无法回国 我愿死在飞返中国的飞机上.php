目前独居在纽约的中国最着名防止爱滋病工作者高耀洁，健康情况欠佳，但她却坚持要在剩下的岁月，继续向外界传递中国爱滋病的真实情况，因为她认为这是对历史的一个责任。<br />
<br />
83岁的高耀洁又说：“我不想老死在美国，但我不认为我可以返回中国，我想我会乘坐一架飞返中国的飞机，然后在飞机上死去。”<br />
<br />
高耀洁去年8月离开中国搬到美国居住，目的是要免受政府的干扰而完成她的着作，纪录她在中国防止爱滋病的多年工作经历，“让更多人了解中国真正的状况”。她说，她的着作在中国虽然被禁，但透过传媒，她希望更多人了解她的工作。她说：“活下去对我是很痛苦的，我活着只是想完成这些着作，因为我可以为历史留下这些纪录。”<br />
<br />
