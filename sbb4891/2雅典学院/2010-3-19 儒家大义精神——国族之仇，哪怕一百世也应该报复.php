													<h2>儒家大义精神――国族之仇，哪怕一百世也应该报复</h2>
						
						
																											<div id="postmessage_522748" class="t_msgfont">儒家大义精神-----国族之仇，哪怕一百世也应该报复!<br />
 <br />
&nbsp; &nbsp; 在南京大屠杀七十周年即将结束的时候，我写下了这些文字，希望以此纪念在伟大抗日战争中在南京、在东北、在华北、在中原、在西南、在江南、在两湖、在华南以及东南亚所有死于日寇屠杀的同胞们。并为此前和之后所有死于异族之手的祖先和同胞们伸张大义，以免使死者痛心于地，生者惶惑于今日。<br />
　　　　<br />
　　“要记住历史，不要记住仇恨”，这是南京大屠杀幸存者李秀英生前留下的话。“可以宽恕，但不可以忘记”，这一类貌似公允、高尚、政治正确的言论成为了主流。很多人在提到南京大屠杀，谴责了一番日本后，最后也念念不忘反对中国“狂热、极端、狭隘”的民族主义。这些言论多么让人痛心，因为这些言论中所表露出来的思维和逻辑都已经说明，许多人的民族精神已经离中华文明何其远也。<br />
　　　　<br />
　　首先，不要妄谈宽恕。宽恕是一个用滥了的字眼，已经快变成纵容罪恶的代称。宽恕也并不等于美德，否则也不会有中山狼的故事。而宽恕更是要看你有没有那个资格和权力，宽恕是自愿的也是个人的，只有个人的宽恕，没有国家和民族宽恕。就象二战后，一个犹太人所说的，“我们没有宽恕的权力”。是的，谁又有代表那些已经不能表达意愿的死难者宽恕的权力呢？<br />
　　　　<br />
　　李秀英从其个人事迹来说，我对她有敬意，因为她敢于反抗日本鬼子。但我不赞同她所谓的宽恕，而且我也明白这种言论很可能是受某些舆论和导向影响做出的。但是谁又有权力代替我们国家和民族宽恕呢？他们以为自己算什么？上帝？他们怎么敢如此狂妄和无耻，代替那些被残酷侮辱和虐杀的同胞们宽恕敌人呢？那些死难同胞们会宽恕日本人吗？我们不知道，也无法知道，因为他们永远表达不了他们的意愿了。谁都没有权力替无数的死难者宽恕谁谁，用自己的无耻强奸死难同胞的意志。<br />
　　　　<br />
　　那些口口声声宽恕和忘记仇恨的人，只能表明他们自己宽恕了日本和其它国仇对中国的破坏，对中国人民的屠杀，他们自己对日本人和国仇们没有仇恨。他们如果有廉耻，就大方的承认这一点，而不要妄加代表。他们既不代表，也没权力代表我们国家和民族。“楚虽三户，亡秦必楚”，同样只要还有一个真正的中国人，这种与中华文明对立的做法就不能被视为中国人的意志。<br />
　　　　<br />
　　这种记住并宽恕的观点，来源于基督教。基督教做为神本的宗教，人所遭受的痛苦以及犯下的罪行与“神恩”相比之下当然是微不足道的。所以多少人的死亡和苦难，从基督教的角度来看，都远不如表达“神无限的爱”，用以表现神对于人的无限主宰能力的宽恕重要。当然，这种蔑视人的价值的虚伪西方逻辑针对的主要也是他人，自己则是另一种标准。我们看看，接受这种观点的是谁？是南非等先是被西方在政治上，最后在精神上奴役的失去文化自主性的民族。<br />
　　　　<br />
　　世界上任何一个强大和自尊的民族和国家都不是宽恕敌人和能忘记仇恨的。罗马在第二次布匿战争中饱受汉尼拔的蹂躏，在战争以后罗马元老加图每一次演讲的结束语都是“一定要毁灭迦太基”，正是这种精神让罗马成为当时能与中国相提并论的世界强国，而也正是丧失了这种精神，改信基督教的罗马被野蛮人轻易的毁灭了。犹太人自二战以后从来没有停止过对敌人的追杀，正是这样，现在以色列才会成为袖珍超级大国，而不是重复被阿拉伯人屠杀的命运。大汉也正是从武帝朝以儒家做为国家主导意识形态后，以春秋复仇大义为名，中止了对匈奴的妥协退让，主动出击，打的匈奴俯首称臣，留下了“犯强汉者，虽远必诛”的豪言。<br />
　　　　<br />
　　现在很多不了解儒家，或者被误导的人。将儒家的庄重有礼当成懦弱，却将粗野放肆当成勇气。真正勇敢的人，不需要在日常中表现的剑拨弩张。因为这种强悍是坚定的、理性同时也是深刻的，因为这种勇气来源于内心深处。所以真正的儒家，可以在平时温文尔雅，在战争中却能殊死战斗到底。那种由野蛮或完全由感情激发的血气之勇，来的快去的快。而儒家内心和理性的勇敢则是坚韧不屈的，这也是在中国与外敌的战争中，抵抗的最顽强，而且坚持到最后的也基本是儒家信徒的原因。这也是在与中国同时兴起的文明、国家、民族相继灭亡后，只有中国和汉族仍然存在的内在原因。<br />
　　　　<br />
　　孔子在很多人眼中是一个慈祥的老师，但孔子并非一味温和，更不会对仇敌宽容。除了现在已经广为人知的，曾经被断章取义的错误引用的“以德报怨，以何报德？以德报德，以直报怨”之外。孔子对复仇极为推崇，了解的人恐怕还不占多数。孔子说，父母之仇，不共戴天。为人子者，必须时刻带着武器，尽一切可能手刃仇人。而对于兄弟之仇，如果突然相遇，既便没带武器也应该上前博斗。当然，这是在没有公正执法的前提下，而且也有限制，就是兄弟之仇如果是正在执行出使国外的任务，则此时不能复仇。这是为了将个人的道义与对国家和社会的责任统一起来。<br />
　　　　<br />
　　而在孔子的重要著作春秋中，一个主要的宗旨就是大复仇。“大”的意思，就是肯定、赞许、推崇。对于春秋社会秩序崩溃的礼崩乐坏，内战不断，儒家思想极为反感，最后总结了“春秋无义战”。即便如此，对于复国、复仇之战，儒家仍特别看待，视为义战。吕氏春秋亦云，战争是人的天性，世界不会停战争，但战争有义战与非义战之别，复仇则为义战，是应该肯定和支持的。<br />
　　　　<br />
　　春秋时期，齐襄公灭亡了纪国。齐襄公是春秋时代的齐国国君，因与亲妹鲁桓公夫人通奸，并谋杀了鲁桓公，最后因为失信而导致政变被杀而臭名昭著。无论是其人品，或是其与鲁国的关系，都是做为鲁国人以及崇尚道德规范的孔子所不会溢美的。但春秋善善恶恶，亦不以人费事。所以既便齐襄公所为悖乱，但春秋仍然肯定了其值得表彰的行为，在这一事件的评价中，给了齐襄公贤者的待遇。<br />
　　　　<br />
　　那么齐国灭纪为何成为春秋肯定的事例呢？齐襄公九世前的齐国国君齐哀公因为纪侯在周夷王面前进谗言而被烹杀。由此引发了春秋对复仇的系统论述。虽然齐襄公行事荒唐，但他牢记祖先并为之复仇，甚至在战前占卜显示要付出军队损失过半甚至自己死亡也不退缩，说明他为自己的祖先尽心。为亲尽心为孝，为国尽心为忠。这就是儒家所要肯定的。祖先所蒙受的耻辱就是自己的耻辱，报仇雪耻既是责任也是义务。<br />
　　　　<br />
　　有人问，此事前后相距已经有九世，难道还可以复仇吗？春秋回答，哪怕一百世也应该报复。因为齐哀公无罪被杀，君主之仇就成为了国仇。除非这个国家消失，否则这个仇恨就会一直存在下去。同样，按儒家“民为贵、社稷次之、君为轻”的观点，国家和民族的仇恨更是国仇，只要中国和汉族还存在，这个仇恨就永远存在，也必须报复。其实，现在国内外反汉势力，尽其所能虚无化汉族，诬蔑汉族的血统和历史传承，或者否定汉族做为中国的绝对主体。也正是日本之类的国族之仇对儒家思想复兴的恐惧，采取的釜底抽薪奸计。通过否定汉族血统、历史和文化的传承，这些对中国和汉族犯下了滔天罪行的异族，想使汉族的复仇丧失基础。用心如此，我们才会明白反华反汉者虽然本族血统构成远比汉族血统杂乱，但总是乐此不疲的诬蔑汉族血统不纯正，他们的真正目的和用意何在。但事实是，汉族的血纯的是纯正的，传承是明确的，而那些对中国和汉族犯有大罪的仇敌，无论经过多长时间，除非它们消失了，否则绝不能逃脱我们的惩罚。<br />
　　　　<br />
　　为什么儒家的复仇欲如此强烈呢？因为儒家提倡仁爱，而且这种仁爱是由近及远，由亲及疏的。只有对亲人有爱、朋友有爱、乡土有爱、国家有爱、民族有爱，并且怀着为国为民的社会责任感，并以大义和公利为平衡的仁，才是一个真正的儒家所追求的仁爱，也是符合人性的爱。而正因为有仁爱，所以才有了孝、有了忠。也因为有仁爱，才会对亲、友、国、族的敌人无比仇恨，这都是儒家思想基础在于人性化的表现，并且将人的理性和感情有机的统一在一起，使人成为更真诚，更高尚的真正的人。<br />
　　　　<br />
　　儒家重视“慎终追远”，因为只承前，才能启后。一个人、一个民族、一个国家只有了解了过去，才能获得经验和力量，开创未来。了解了历史，才会对我们的身份和作用以及肩负的使命有清醒的认识，并且获得借鉴和增强民族责任感。而这正是中华文明对历史真相极度重视和尊重的原因，这也是中华文明和汉族区别于许多文明和民族的地方。当然，这种对历史真相的尊重的文化传统，在满清和后来受到了极大破坏，因为反汉专制者，不想让汉族人明白自己的历史，增长自信和智慧，尊严和勇气，而是想将中华文明的继承者变成愚民和奴才。但就象黑夜无法永远遮住太阳，龙终究不会被狼和狗们所压倒。<br />
　　　　<br />
　　这种对历史的重视，也是为了向祖先表达恭敬之心，承担华夏一脉相承的责任和文明，以不辜负先辈之功业，方能继往开来。对于给我们国家和民族犯下大罪的国家或民族，我们如何与之相处？两国相处必然不能回避历史，自古以来友好交往的谎话只能自欺欺人。除了表明自己懦弱和阿Q，只会让别人耻笑和轻视。除此之外还会起什么作用呢？既然不能忘记历史，我们又如何不对历史动感情，难道我们是木头人和石头人吗？我们是人，就会有人性，就会有感情，就会对我们的仇敌报有仇恨。在深仇大恨之下面对国仇却要装做若无其事，这样真正热爱中国的人会何其痛苦。所以，有深仇大恨国家和民族之间的关系注定是不会友好的，除非举国没心没肺。正因为如此，所以儒家认为，与其压抑自己，徒增痛苦，而又无益于事，同时损害了国家和民族的精神。不如以一个真正人性的立场对待，所以儒家认为与国族之族不必、不应、也不能共存。<br />
　　　　<br />
　　现在很多人的谬论，就是罪行是过去所犯，这个责任随着当事人的死亡就已经了结，对曾经对中国和汉族犯罪的民族的后代不应受过。这种谬论流传甚广，更可见不学无术和无知的危害。春秋中也有这种疑问，认为九世后的纪国不应该为祖先受过。春秋对此的回答非常明确：必须承受！<br />
　　　　<br />
　　因为按照以直报怨，如果纪国当时所犯的罪行马上得到公正的惩罚，纪国就不会存在。而一个本不应存在的国家，在九世之后才被灭，又有什么可报怨的？任何一个国家和民族都不是凭空冒出来的，必然继承了其祖先给其留下的遗产。既然承担了祖先的遗产，就必须承担其遗产的后果。对于那些屠杀数以千万、百万我们同胞的异族，如果当时就能给予同等报复，这些民族应该全部被灭，岂能存留至今？一些本来就应该灭绝和灭亡的民族和国家，他们能生存至今已经是非常过分的幸运了，怎么会有人为向其复仇叫屈呢？<br />
　　　　<br />
　　当然，肯定会有人来转移问题，反问我们，为何汉族内部的矛盾不应不延续千古？儒家对此也做出了解答，这种百世复仇只限于国族之仇。民族内部的仇恨则是不能无限期的，因为民族国家内部矛盾可以通过公正的法律来解决，解决不了，进行复仇也应该只及其身而不诛连蔓延，这是区别内外的民族共同体内部的社会道德和民族认同，当然不能与国族之外一概而论。<br />
　　　　<br />
　　春秋，君主和父亲无罪被杀，而臣和子不能为之复仇的。君父就不能称为“葬”，表明其无臣无子。同样，一个国家和民族的死难者，如果国家和民族没有为他们复仇，他们也不会安息，所以南京和抗战中所有的死难者，以及之前和之后死于异族之手同胞们的灵魂仍然不得安宁，说明国家和民族还没尽到自己的责任，是有愧于死者的。那些不为君主和父亲复仇人没有资格称自己为臣、为子。那么不为人民复仇，又有什么资格称为中华文明、中国的继承者和人民的代表呢？<br />
　　　　<br />
　　当然，也会有人说，现在我们国家还没有足够的能力，空谈复仇又有何用？与世界其它与中国有仇恨的国家和民族同时搞的剑拔弩张对国家难道不是有害无益吗？ “理有经，亦有权”，在坚守原则的同时，当然不妨灵活应变。俗话说“君子报仇，十年不晚”，春秋之仇可以复于九世之后。但是，这不是为了给自己制造苟安的借口，在这一过程中我们必须牢记仇恨，并为复仇而努力，并最终实现。只有复仇最终实现，才能证明我们国家和民族的继承了华夏祖先的大义。<br />
　　　　<br />
　　现实国际关系的政治变动中当然有权宜之计，这个民众对于形势所迫下的某些与大义不符之处可以容忍和凉解。但大义本身和民族精神不能动摇，如果为了短浅的权宜，就来扰乱和动摇民族精神和大义，则说明这不是真正从国家民族角度出发的策略，而是决策者的懦弱、无耻、卑鄙、愚蠢。我们民族精神和文明是八千年来华夏祖先们用智慧、勇气和坚韧奠定的，不能与一撮人渣的短浅之见随风起舞，而不断摇摆。一个国家和民族没有真正坚定立场及精神，不可能真正稳定和强大。“人而无信，不知其可”，一个政权如果没有固定的信念和信用，朝令夕改，朝敌暮友，又如何让民众对其有基本的信任？问政于孔子，孔子认为在不得已的情况下，可以减少军备，甚至可以减少食品等基本物质需求，但“人皆有一死，民无信不立”，一个政权，本身没有信念，让民众也没有信念，本身没信用，让民众对其丧失了信任，总是要垮的。<br />
　　　　<br />
　　儒家精神自满清以来，一直被压制和歪曲，导致了其后中国精神日益不振。许多人身上奴性太重，没有自主意识和人格，丢掉了做人的尊严，完全拜倒在外来思想的脚下，丧失了独立和理性思考的能力。却反过来否定自己不了解，也不想去了解的中华文明。这种情况继续下去，中国只会进一步沦为被外国压榨和掠夺的国家，成为世界的血汗工厂，广大的中国人和中国人的主体汉族只能成为外国资本和一小撮买办的血汗劳工，并成为被牺牲的对象。中国必须进行一番文明、历史观、民族精神、理性哲学、伦理道德的真正复兴，这将是一个比欧洲文艺复兴伟大的多的真正文明之复兴。</div>

							
							
							
							
