													<h2>莫之许：除了成功 别无信仰</h2>
						
						
																											<div id="postmessage_730800" class="t_msgfont">提要：至少在当下的阶段中，追逐现世功利仍将是主要的社会潮流，对于在此潮流中浮沉的芸芸众生而言，打动他们的更多是那些鲜活的成功案例，而不大可能是什么新的价值理想，至于价值理想与现世功利之间新的平衡的建立，注定将是个漫长的过程。<br />
<br />
方舟子披露&quot;打工皇帝&quot;唐骏学历可能造假的微博, 终于由茶杯里的风暴演变成为&quot;学历门&quot;, 尽管事态发展看上去越来越不利于唐骏,但公众评价却并非如此,网民似乎并不会为唐骏是否拿到学位，抑或专利权是否属于他本人这类事所扰。截止于周二晚，近日的一个在线调查表明，4760名网友中约76%都认为个人能力远比学历重要，只有不到1%的人持相反意见。除学历比能力重要论之外,尚有作假之人太多，没必要单挑唐骏论、成功更重要论等等，综合而论，不妨名之曰，只要成功，一切皆OK。<br />
<br />
早有论者指出，以儒家文化为主流的中国文化更推崇现世的价值，经典中随处可见此类论述，如“太上有立德，其次有立功，其次有立言，虽久不废，此之谓不朽”，此外如修身、齐家、治国、平天下、”达则兼济天下、穷则独善其身“，无一不指向于短暂一生中的成就和作为，将之作为人生价值的体现。对于普通百姓而言，或许并无此等抱负，但人生价值的寄托也与之有所同构，即都将此生或现世的成就和作为置于优先位置，消极者如“人死如灯灭”、“好死不如赖活着”，积极者则追求“吃香喝辣，大秤分金、大块吃肉”，希望轰轰烈烈地牛一把。因此，推崇成功，不及其余，实为中国人价值观内的“深层结构”，鲜有人能出其外。<br />
<br />
中国传统文化尽管推崇现世价值，背后仍有超验的理想或者说超越的价值作为后盾。对精英而言是所谓天道, 于百姓而言则所谓&quot;举头三尺有神明&quot;，无论是经典论述，又或是百姓日常奉行的伦理，均含有类似劝诫。因此，虽推崇现世功利，也需要遵循一定之规，才是中国传统社会的常态。然而，这种平衡很是微妙，曾有汉学家指出，以现世功利为优先的中国社会和以基督教的美国社会一样，都有若干基于价值观的道德模范。不同的是，基于宗教信仰所形成的普遍约束，在美国社会遵循这些模范所体现的价值如诚实无欺（华盛顿与樱桃树），又如刻苦上进（林肯的小木屋），很可能会获得社会意义上的成功，而在中国社会遵循这些模范，如卧冰求鱼，又或者如割股疗亲，则可能会付出无可挽回的惨痛代价。因此，中国社会的道德劝诫多半是指导性的，且往往同时存在着近乎矛盾的理想和现实两种指导意见：既有忠贞不二，又有良臣择主而事，既有宁为玉碎，又有留得青山在，这就使得包括诚信在内的诸多道德规范，并不具备压倒现实功利的超然地位，当方舟子们用诚信这一价值规范质问唐骏时，其实隐含了诚信的超然地位，而很显然，绝大多数当代中国人却未必也这样认为。<br />
<br />
方舟子们与普通网友之间的落差, 可以代换为接受现代(西方)文明洗礼的部分中国人,与大多数依旧沿用传统文化的中国人,在价值理想与现实功利的关系上的差异。前者更认同价值与事实的二分，并坚持用价值评价事实；而后者则认为，价值与事实很难分开，尽管后者也多半承认诚信是一种价值，但同时也倾向于认为，成功本身也是一种价值，在很多时候，成功甚至是一种更高的价值。当方舟子们以诚信相质问时，不啻是说，不诚信的成功是可疑的,不值得推崇；而同情唐骏的声音则认为，成功本身就说明了其价值，至于不诚信，不过是对这一价值的一点小小的玷污而已，不值得大惊小怪。或许，令方舟子们始料未及的是，即使在经过这么多年的西风东渐，持有与他们相似的价值与事实二分的世界观的中国人，依然是那么的稀少。在和菜头等人的喟叹中，我分明感受到了这种失落。<br />
<br />
而在我看来, 出现这样的落差并不希奇,而且恰恰与西风东渐的大变局密切相关。如上所述，传统社会在价值规范和现实功利之间维持着一种看似矛盾但又很微妙的平衡，这离不开儒、道、释三家所提供的超验的理想或者说超越的价值，也离不开与这些价值理想互为表里的传统社会结构，而近代以来，中国社会面对西方挑战的剧烈变迁，已然摧毁了延续千年的传统社会结构，而全能主义社会所强行推广的唯物主义教育，更将传统的价值理想一举扫除，在这种情况下，既有的微妙平衡荡然不存，现世功利势必成为压倒性的追求目标，近年来,许多人感叹社会上物质主义泛滥，享乐主义猖獗，其实皆与此种平衡的打破有关。毋庸置疑的是，至少在当下的阶段中，追逐现世功利仍将是主要的社会潮流，对于在此潮流中浮沉的芸芸众生而言，打动他们的更多是那些鲜活的成功案例，而不大可能是什么新的价值理想，至于价值理想与现世功利之间新的平衡的建立，注定将是个漫长的过程，对于活在这个时代的大多数人来说，或许难以避免采用以下的立场, 那就是: 除了成功，别无信仰。</div>

							
							
							
							
