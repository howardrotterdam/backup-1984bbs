													<h2>可以同时信仰儒家和其它宗教吗？</h2>
						
						
																											<div id="postmessage_827010" class="t_msgfont">可以同时信仰儒家和其它宗教吗？<br />
<br />
或问：可以同时信仰儒家和基督教或者同时信儒佛道三教吗？<br />
<br />
东海答：当然可以。信仰什么不信仰什么，怎么信仰，在信仰上脚踩几只船，是真信、假信还是半真半假的信，信仰的目的是什么，等等等等，都是每个公民的权利。只不过，真正建立了儒家信仰（良知信仰）的人，是不会同时脚踩两只船或多只船的。<br />
<br />
信仰具有一定的排他性。不同学说、宗教之间，或有相通相同的地方，但在根本处必然有异。象儒佛道三家，很多地方相同，根本处相通，都重心性修炼，都持“心物一元”、“体用不二”论（具体理解有所不同），但入世出世立场大不同。佛道同为出世法，但对心性的认知和修炼的方式都很不同。<br />
<br />
良知信仰同样具有一定的排他性。其它宗教的信徒在放弃其原来的信仰之前不可能信仰儒家（如有人这么说，必是把一般的好感、喜欢错当做信仰了。）身为儒者而表示信仰其它宗教，更说明其人对儒家没有真正懂进去、搞明白。<br />
<br />
儒家“极广大而尽精微，极高明而道中庸”，同时具有思想性、政治性、道德性、科学性、真理性和宗教性等等特性，可谓不是宗教胜似宗教。真正把吃透了孔孟之学中庸之道，解悟了良知本性的奥秘，责任大智大开一切无惑，不可能再把其它宗教放到信仰的高度。<br />
<br />
信仰的心灵和精神的最高寄托，是个体安身立命的内在栖居。究极而言（从根本上说），在信仰上脚踩两只船或多只船，就算不上真有信仰，或者说属于低层次的信仰。至于迷信（迷迷迷糊糊地迷着信）、伪信（装出信的样子其实不信）、功利性的信，更是对信仰的亵渎。<br />
<br />
古代不少士大夫往往儒道双修、儒佛双修乃至儒佛道三修，论其根本，终属儒家。一些佛道门中人爱好儒家，终究有主次之分。象东海，一向儒佛道三修，但到了真正领略到儒家的优秀、证悟到良知的殊胜的时候，想不皈本于儒家，不可得也。尽管对佛道两家仍然十分喜爱尊重，但不会把它们“提高”到信仰层面。对儒佛道同样喜欢或者在三家之间彷徨的时候，也是我义理不明、觉悟不高、大本未立的时候。<br />
<br />
另外，儒家在政治制度层面（外王层面）主张“道并行而不相悖”，充分尊重各种异端外道的言论、信仰自由，但在义理上极为“严厉”，分毫必争寸土不让。<br />
<br />
至于现代儒家对其它宗教不同程度的认可，也是自然。批异不碍求同。现代儒者对佛道的认可程度往往较高，因为相同的地方多；对基督教的认可程度普遍较低，因为相通的地方少。基督教神本主义的蒙昧、人神割裂的粗陋以及上帝创世造人之说的荒诞，在略有中华文化修养及现代科学常识者眼里，都是一目了然的。<br />
<br />
对于那些表示同时信仰儒家和基督教以及其他拥有两个以上“信仰对象”的人，蒋庆先生在《基督信仰与中国文化》中有一段话堪称振聋发聩，特录于此谨供参考：<br />
<br />
“我们知道，生命信仰是整个生命的投入，是全部心灵的归依。因此，信仰的对象只能是一，而且必须对此一信仰对象保持绝对的忠诚。因为只有这样，生命才能得到安立，心灵才能得到宁静；如果有两个信仰对象并且对此二者都保持同样的忠诚，势必生命不能整个地投入，心灵不能全部地归依。在这种情况下，生命就会依此失彼，依彼失此，不知何去何从，心灵就会焦虑不安，左右为难，永远处于冲突状态。在生命的信仰上之所以会要求绝对的排他的忠诚，是出于生命的真诚与感情的专一，而不是出于理智的选择与意志的决断。当然，对某一生命信仰的忠诚并不意味着要排斥其它的生命信仰，而只是表示信仰者的整个身心性命投在此而不投在彼。具体说来，在当今的中国（包括港台），基督信仰与儒家信仰在争夺着人们的忠诚。作为一个中国人，面对着基督信仰与儒家信仰，只能归宗其中之一，而不能同时对二者都表示忠诚。这是非此即彼的选择，必须做到绝对的真诚。一个中国人选择了基督信仰，当然可以同时对儒家信仰表示同情与理解，并且可以在很多问题上认同儒家的精神价值，但这毕竟是第二等意义的事了，因为他的整个生命已经在彼而不在此了，他的全部存在已经以第一等意义的事为转移了。反之，中国人选择了儒家信仰，对待基督信仰的态度亦然。总之，在生命信仰问题上不可能存在着两种忠诚，尽管有许多中国人企图在归宗基督的同时又保持对儒家的信仰，但这实际上是达不到的、这除了在心中引起无穷无尽的焦虑与冲突外，所表现出来的只是对两者都不忠诚。”<br />
