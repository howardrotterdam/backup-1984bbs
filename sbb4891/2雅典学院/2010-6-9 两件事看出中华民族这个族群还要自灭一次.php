　　“其实最早我们做了两个方案，比如没人理这个帖子怎么办？火爆了怎么样？只不过现在这么热，有点始料未及。”该公司CEO黄亮华今年27岁，他的公司有10名员工，每年这样的“炒作”业务，大概有3到5个，“平时常规的营销比较多。炒作有特定的客户要求”。黄亮华透露，该事件他们进行了两个月的反复思量和流程设计，动用了四个执行席媒介，轮班监测执行情况，两小时一次电话汇报。“总计动用网络营销从业人员800余人，注册ID2万余，回复10万余。”而大量的网络任务是交给兼职的网民去完成的。在引发轰动效应后，策划团队撤出，他们的回帖只占真正网络回帖的3%。<br />
　　一个创意赚了“6位数”<br />
　　接受记者采访时，黄亮华表示不方便透露是谁让他制造了这场“炒作”，但他承认，与游戏相关。“我们都知道‘魔兽世界’这个游戏停了一段时间，魔兽用户都没事儿干，所以我们需要激活贴吧的用户，制造一个事件，让他们觉得不无聊。其次希望吸引社会上不玩魔兽的人也关心它。”<br />
　　说起这次炒作的收入，黄亮华说他共赚到了“6位数”的酬劳，他认为这不算很高。“相比起来，这个事件的曝光率很高，美国方面都有报道和评论。”<br />
　　“贾君鹏”是虚拟人物<br />
　　贾君鹏这个网名，是根据黄亮华给朋友的孩子起的名字“李君鹏”修改的，因为李姓太多了，为了防止跟众多的真名重名，就姓了贾。“我们考虑要熟悉一点，好记。并且‘贾’通‘假’字，就是暗示是虚拟的人物。”<br />
　　至于“你妈妈喊你回家吃饭”那句话，他说：“其实很容易，也没有文字上的难度，这个创意，多少有点像唤起童年记忆的意思。<br />
　　我们这个年龄，成年以后，很少有母亲大声叫你回家吃饭。可小时候我们在草坪上玩泥巴，母亲远远地就来喊了，所以说这句话的时候，内心的感情一下就涌出来了。现在母亲叫吃饭顶多打个电话，孩子也很少有玩的时候。网络上有回归童年热的情况，我们多少借鉴了一些。”他表示后续还有一些精彩的升级。<br />
　　不认为是欺骗网友<br />
　　一些网友在听说“贾君鹏”是策划出来的后，表示很失落。当记者问黄亮华，如此炒作算不算一种欺骗？他停顿了一下说：“这要看怎么理解了，现在的网民娱乐级别比较高了，能够自娱自乐，不就是玩嘛，也都玩得起，所以不存在什么欺骗。而且最后我们愿意把这个事情披露出来，就是不愿意把网民蒙在鼓里，让网民觉得太委屈。”<br />
　　黄亮华毫不避讳地说，这样做也会对公司知名度方面有好处，“各方面的需求都有吧。”</div>

							
							
							
							
