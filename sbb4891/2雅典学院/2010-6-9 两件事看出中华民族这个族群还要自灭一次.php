<br />
第一件事：<br />
&nbsp; &nbsp; 6月7日晚8时多，东城牛山堑头新兴工业区门口发生惊人一幕：一男子眼睁睁看着自己年轻的老婆被一群歹徒掳走，事后不知去向。<br />
　　目前，东莞市公安局刑警支队、东城分局刑警大队已介入调查，开会研究了一个下午，警方正加紧调查。<br />
　　昨日下午，记者来到东城牛山堑头新兴工业区门口，目击者刘先生向记者回忆了当时的情况。<br />
　　“昨晚8时45分左右，一男一女骑着小单车，来到我摊位买东西。”陈先生说，正在此时，一辆面包车突然一个急刹车停在他摊位前。“车还没停稳，车门就打开了，下来三个穿着黑衣服的男子，捂住这个女的嘴巴，一把架起就往车上拖。”陈先生说。<br />
　　目击者陈女士也说：“当时，那个女的紧紧攥住丈夫的手不放，她丈夫也使出全身气力把老婆往回拖。无奈，女子最终被拖上了车。”<br />
　　歹徒抢到了人后，一脚踢开女子丈夫，关上车门，绝尘而去，整个过程不超过两分钟。<br />
　　事发后，女子的丈夫打了摩的去追那辆面包车，但最终没有追到。之后，男子向当地的同沙派出所报了警。<br />
　　而在新兴工业区附近，也有不少开店的人说见过小两口，经常下班后骑着单车前来购物。“这个女的姓赵，20多岁，年轻漂亮，身高一米六多，可能来自湖南。”一小卖部的阿姨向记者透露。<br />
　　据悉，被掳女子和现在的丈夫有一个两岁多的孩子，居住在附近。不过，昨日记者未能找到该男子。<br />
