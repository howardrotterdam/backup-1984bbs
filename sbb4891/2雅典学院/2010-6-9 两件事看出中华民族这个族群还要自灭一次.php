&nbsp; &nbsp; 5月16日晚，赴美仅两个月的23岁中国女留学生姚宇在法拉盛繁忙街区（法拉盛是个华人新移民聚居区，基本上都1949年后的大陆移民）被一名墨西哥裔男子拖入后巷强奸，并被一根金属管狠狠敲打头部至脑死亡。据美国《侨报》报道，案发地点在法拉盛颇为繁忙的41路的一民宅前的停车位内。据负责此案的纽约市109分局警探Jae Shim介绍，犯罪嫌疑为28岁的墨西哥人Carlos Salazar Cruz，目前还不清楚他是否有前科。作案时，他使用一金属管打击姚宇的头部，再把她拖进一住宅前的停车位内实施了性侵害。案发后不久，警方在离现场不远的41路上抓住了他。据悉，有人在第一时间报案，帮助警方抓住了嫌犯。<br />
