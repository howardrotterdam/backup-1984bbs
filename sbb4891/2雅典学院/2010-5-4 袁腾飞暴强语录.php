袁腾飞：中国已经具备了两院制国会的雏形，政协就可作为上议院或参议院，人大就可以作为众议院，美国众议院U.S. House of Representatives直译过来就是人民代表大会，但美国的议会权力是很大的，中国的议会也有用，人大可以举手，政协可以拍手。<br />
