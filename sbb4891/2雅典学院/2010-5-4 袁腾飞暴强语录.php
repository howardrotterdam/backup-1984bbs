袁腾飞：毛泽东当年在北大一个月挣8块，那些教授都好几百，他眼气，对知识分子刻骨仇恨，所以知识分子也该刻骨仇恨他。哪个读过书的人如果认为他伟大的话，那你这书都读到狗肚子里去了。<br />
