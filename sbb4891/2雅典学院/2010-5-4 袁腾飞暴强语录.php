<br />
 <br />
<br />
袁腾飞：如果八大“以经济建设为中心”的路线没有被毛泽东篡改的话，一直坚持的话，（搞市场经济，不搞计划经济）1956年到现在，我们今天的发展水平会是什么样？我们就是不能跟四小龙比，因为我们人口实在太庞大了，怎么着跟泰国马来西亚这些国家能有一拼吧，你现在呢？当然我们的城市是要比他们强太多的。<br />
<br />
 <br />
<br />
袁腾飞：你们80后90后都是很幸福的一代，生长在尧天舜日的环境当中，没有经历过红色恐怖的时代，没有经历过中华文明历史上最黑暗的时代。中国1956―1976那二十年，也就跟纳粹那12年有一拼！对人类践踏的程度，超乎人类想象。<br />
