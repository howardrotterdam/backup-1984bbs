<br />
 <br />
<br />
袁腾飞：罗斯福说人最大的四个基本自由，第一个就是言论的自由，还有信仰的自由，免于饥饿和免于，惧的自由。言论自由属于天赋人权，我长着张嘴不是国民党让我长的，也不是共产党让我长的，是造物主赐予我的，除了吃就是说，光吃不说那是哑巴。<br />
<br />
 <br />
<br />
