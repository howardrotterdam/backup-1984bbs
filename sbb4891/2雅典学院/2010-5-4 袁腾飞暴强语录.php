<br />
 <br />
<br />
袁腾飞谈毛泽东“反右”：毛泽东说了，我们不能学斯大林，斯大林杀人太多，我们不杀人，我们留着那帮人，“让他们为人民生产小米嘛”。-------- “士可杀而不可辱”，你这么干更缺德，丧心病狂。<br />
