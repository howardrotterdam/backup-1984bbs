<br />
 <br />
<br />
（人民解放战争、过渡时期方面）<br />
<br />
 <br />
<br />
袁腾飞：毛泽东自己讲嘛：“抗日战争急不得，解放战争拖不得”，，抗日战争这玩意是打消耗的事，谁急谁先死；解放战争这是抢天下啦，这不能拖这个！中国人的这个战争的特点在两场战争里体现的非常清楚：“外战外行，内战内行”，打自个人都厉害着呢。<br />
<br />
 <br />
<br />
袁腾飞：解放战争国民党怎么打共产党，共产党怎么打国民党啊，一场民族的悲剧，没有什么可说的！<br />
<br />
 <br />
<br />
袁腾飞谈国共内战：小日本八年侵略战争完事之后，人家就开始进行建设，我们完事之后接着自己人叮叮堂堂更热闹，小日本没打着的地全打着了！四川，小日本没打着吧，内战全打着了。为什么呢，当然国民党赖共产党，共产党赖国民党。<br />
<br />
 <br />
<br />
袁腾飞：世界史大家有没有印象？二次大战之后确立了雅尔塔体系，根据雅尔塔体系，美苏两国平分天下，当时中国是美国的势力范围，美国当时对中国的态度是很捧啊，要不我们这样一个国家凭什么跟苏美英法并列联合国五常啊？<br />
<br />
 <br />
<br />
袁腾飞：当时美国希望中国代替日本成为亚洲老大。美国邀请中国英国新西兰澳大利亚和印度去执行占领日本的任务，当时给我们划定的占领区是四国岛，中国都派出了一支军队，连牌子都挂出来了：中华民国驻日占领军司令部，准备去占领日本，结果这支部队最后上了船却去了沈阳，打内战去啦！<br />
<br />
 <br />
<br />
袁腾飞：美国为什么要扶蒋反共，共产党跟他有仇吗？共产党挖他家祖坟了吗？美国认为，如果中国被共产党占领的话，就会倒向苏联。这是因为美国队中国共产党的领导人毛泽东不了解的：<br />
<br />
 <br />
<br />
袁腾飞：毛泽东是什么人啊，全天底下古今中外谁他瞧得上眼啊，你看毛泽东写：“数下流人物，还看今朝”<br />
<br />
 <br />
<br />
袁腾飞：毛泽东1949年建国以后被迫到苏联拜码头，朝见斯大林。他心里其实是很不痛快的。毛泽东认为他才是国际共运的龙头老大，他应该是帮主，大家应该拜他。结果苏联不尿他，他急了，结果后来跟苏联掰了。<br />
<br />
 <br />
<br />
袁腾飞：蒋介石政府是政府，毛其实也一样，两个独裁者是一回事。<br />
<br />
 <br />
<br />
袁腾飞：美国要是支援盟邦，那是百分之百铁了心的支持。当时美国给国民党政府提供了59亿美元的援助，马歇尔计划给欧洲一百多亿，都是白给，无偿的。美国是把盟国看成朋友，苏联是把盟国看成奴才！<br />
<br />
 <br />
<br />
袁腾飞：老有学者在那里喷粪：“美国打伊拉克是为了石油”，您识数吗？美国在伊拉克打仗花了多少钱？要用那钱买石油能买多少？伊拉克的油值那么多钱吗？<br />
<br />
 <br />
<br />
 <br />
<br />
袁腾飞：1949年毛泽东去拜码头朝拜斯大林，装了半天孙子，弄回来3亿美元，3亿美元是什么概念？苏联红军在东北烧杀抢奸给我们造成的损失是8亿美元，还没有苏联红军在东北抢的钱多呢！<br />
<br />
 <br />
<br />
袁腾飞：美国是把盟国看成朋友，苏联是把盟国看成奴才！<br />
<br />
 <br />
<br />
袁腾飞：1950年毛泽东为什么那么愣，要打抗美援朝，把他儿子都折进去啦，他就是要让老百姓看：爷厉害，敢打美国！打完之后，打的自己断子绝孙了吧。<br />
<br />
 <br />
<br />
袁腾飞：重庆谈判内容非常务虚，就是走个过场！<br />
<br />
 <br />
<br />
袁腾飞：在中国这块土地上你、只能产生专制，从大禹王治水到现在，几千年了我们已经习惯了服从一个人，中国人要是隔几年不喊万岁，中国人觉得很空虚，没法过了。<br />
<br />
 <br />
<br />
袁腾飞：毛主席基本上是中国最后一个皇帝，蒋介石在台湾去世的时候，台湾报纸讲的是“先总统蒋公崩猝”，什么人才崩猝啊？“先帝创业未半而中道崩猝”，蒋介石的遗体在慈湖灵寝，什么人的遗体在灵寝啊？毛和蒋都是最地道的中国人，天无二日，国无二主，有我没你，都是这种人。<br />
<br />
 <br />
<br />
袁腾飞：毛泽东这人干什么什么不灵，有一样东西他特别会，就是打仗，整人呐，他斗人的功夫在中国的历史上是没有见过他这么会斗人的，你让他治国抓经济那完了，这国家非让他抓到原始社会去不可！<br />
<br />
 <br />
<br />
袁腾飞：据说毛泽东会写诗，要是李白是一的话，毛泽东是负一亿。据说毛泽东会写字，反正比江泽民的能强点！据无耻文人们说，据无耻御用文人们说，像郭沫若这样的！<br />
<br />
 <br />
<br />
袁腾飞：我有身份证，我只是中华人民共和国的公民，但中华人民共和国可以不拿你当人民，人民这个词是很有伸缩性的：抗日战争的时候为什么不没收地主土地啊？你得留着他，等抗战一结束，卸磨杀驴了就，你得把土地给人民。抗日战争的时候蒋介石都是人民，抗战一结束，他就变成人民公敌了。<br />
<br />
 <br />
<br />
袁腾飞：国共内战时，北平市和平解放，千年古都得以保存，但六十年代就拆光了，要不洋人给咱国家起得名好啊：China = 拆呢。你说北京是三千年的古都，别扯了，瞅着就像最近20年的事。<br />
<br />
 <br />
<br />
袁腾飞：国民党当年失败的原因就不好多说了，你说了容易对今天的政局产生影响。<br />
<br />
 <br />
<br />
袁腾飞：北京有个地儿叫隆重，所有的会都在那儿召开。<br />
<br />
 <br />
<br />
袁腾飞：毛泽东纪念馆是一正局级单位。<br />
<br />
 <br />
<br />
袁腾飞：49年第一届政协会议选举国家领导人，毛泽东为主席，然后一串副主席，一串副主席仨共产党，仨民主党派，你不能全是共产党当主席，你总得有人鼓掌吧，自个儿给自个儿鼓掌多没劲啊。你唱完了自己来一句“好”，不合适啊。<br />
<br />
 <br />
<br />
袁腾飞：事实上，民国建立以来西藏一直处在一个半独立的状态，他甚至还有自己的国旗：雪山狮子旗。<br />
<br />
 <br />
<br />
袁腾飞：实际上你看今天美国国务院有关朝鲜战争的所有机密文件全都揭秘了：你一看，美国当年没这个意思说要打中国。<br />
<br />
 <br />
<br />
袁腾飞：如今的台湾人民生活富足，安定祥和，古风犹存，是中华道统之所在。 <br />
<br />
 <br />
<br />
袁腾飞：朝鲜战争中国志愿军阵亡11万多人，美军阵亡是3万多人。<br />
<br />
 <br />
<br />
袁腾飞：美国打越战根本没法打，你说你打德国你给他炸回到石器时代，德国就完蛋，越南他本来就是石器时代，你怎么炸啊这玩意儿。越落后的国家越不可战胜。<br />
<br />
 <br />
<br />
袁腾飞：我记得去泰国，第一次去，曼谷真破啊，相当于北京90年的水平，第一次去，怎么感觉像北京88年的水平，我问那个导游，你们怎么不修个立交桥啊？车堵得这么厉害！那导游说了一句话，给我乐喷了：“大哥，这里不是共产党，人家的房子不能说拆就拆啊”。<br />
<br />
 <br />
<br />
袁腾飞：1950年镇压反革命，一年枪毙了71万，逮捕170万，监视居住117万。一下大家对共产党就服了，不服下一个就是你！蒋介石“四一二”才杀多少人啊？有名有姓的才几百个。<br />
<br />
