 <br />
<br />
袁腾飞：蒋经国说：“我们两次跟共产党合作都吃了大亏：第一次国共合作，他四百多人发展到四万多人；第二次国共合作，他军队由三万到一百三十万。最后我们江山都丢了”。<br />
<br />
 <br />
<br />
袁腾飞：你们有没有注意新闻，现在基本上都不提“一国两制”了。“一国两制”在台湾的认同率基本上为零，没有一个人认同。<br />
<br />
 <br />
<br />
袁腾飞：台湾的中华民国全图还包括外蒙古呐，他都不承认外蒙古独立。<br />
<br />
 <br />
<br />
袁腾飞：现在马英九提出来的词儿特别好----“互不否认”。你不承认不要紧，不要否认。<br />
<br />
 <br />
<br />
袁腾飞：你到欧洲你看，好多大饭店门口都挂着青天白日旗，很多欧洲人仍然以为这个是中国国旗。我们去德国访问，德国一所中学欢迎我们挂的是青天白日旗，我们傻了都。<br />
<br />
 <br />
<br />
袁腾飞：实际上跟台湾的统一将来最可能采取的方式就是欧盟式的那种方式，就是双方谁也离不开谁了，自然就合上了。<br />
<br />
 <br />
<br />
袁腾飞：和台湾统一了，国名国都国旗都是可以商量的，国名可以改为中华人民共和民国，国旗是青天白日上镶五个星，国都咱冬天在台北，夏天在北京。<br />
<br />
 <br />
<br />
袁腾飞：五星红旗从美学的角度上看特别难看。<br />
<br />
