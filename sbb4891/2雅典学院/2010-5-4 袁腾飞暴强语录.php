 <br />
<br />
袁腾飞：你看哪个国家学苏联学出好来了，苏联这个国家的核武器可以把人类毁灭几十次，而人民连每天所需要的面包都吃不上，这个政权早晚得完蛋，你坦克比土豆都多，火箭比香肠都多，老百姓不能天天吃火箭，过年再放个原子弹・・・<br />
