袁腾飞：满族基本上已经完全汉化了，溥仪连满语都不会说了。<br />
<br />
 <br />
<br />
袁腾飞：云南纳西族到现在还是原始社会那种母系氏族，家里一切大权都是女的做主，什么活都是女的干，地里活，喂牲口都是女的干。男的没事干，抽鸦片，打麻将・・・・・・<br />
<br />
 <br />
<br />
袁腾飞：中国少数民族被分的特别碎，“众建诸侯而少其力”・・・中国为什么没有大的民族问题？汉族人数占了绝对的优势。</div>

							
							
							
							
