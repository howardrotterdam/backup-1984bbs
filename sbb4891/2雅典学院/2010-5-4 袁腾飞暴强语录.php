袁腾飞暴强语录 第六季<br />
<br />
 <br />
<br />
（抗日战争）<br />
<br />
 <br />
<br />
 <br />
<br />
<a href="http://www.tudou.com/programs/view/JtoAJxGQqwk/" target="_blank">http://www.tudou.com/programs/view/JtoAJxGQqwk/</a><br />
<br />
 <br />
<br />
袁腾飞：卢沟桥事变之后蒋介石说：“我们中国是一个弱国，不是强国，弱国的态度应该是应战，而不应该是求战，牺牲未到最后关头，绝不轻言牺牲；和平未到绝望时刻，决不放弃和平”・・・我们今天反过头来换一种思路思考，如果在战与和之间有选择的话，那么负责任的统治者肯定是应该选择和，而不应该选择战。<br />
<br />
 <br />
<br />
袁腾飞：抗日战争时期，中国士兵落后到什么程度？蒋介石中央军的士兵进入上海之后，他拿灯泡点烟，当兵的连电灯都没见过。<br />
<br />
 <br />
<br />
袁腾飞：抗日战争初期，小日本拼刺刀，一个日本兵可以轻而易举地拼倒三四个中国兵，因为他们是受过严格军事训练的武士，而咱们中国兵基本上都是穿军装的农民。<br />
<br />
 <br />
<br />
袁腾飞：你看咱中国的这个战争电影，尤其抗战电影，军事专家说那都是儿童片，根本就不现实。歪把子机枪一个人根本打不了，你看电视和电影里的八路军从来都是一个人抱着歪把子打扫到一百多鬼子・・・你丑化敌人其实是丑化你自己，要是敌人那么好打，那么怂，你怎么挨了八年揍啊？<br />
<br />
 <br />
<br />
袁腾飞：抗战时期新四军全军10500人，6200支枪，这6200支枪里相当多的是村里做的土枪。<br />
<br />
 <br />
<br />
袁腾飞：抗战时期国军士兵打仗尤其是中央军是很勇敢地，不怕死，那些汉奸伪军其实都是冯玉祥的部队，不是国军。<br />
<br />
 <br />
<br />
袁腾飞：什么样的政府是可以把群众武装起来的？前提是这个政府一定是人民选举出来的，他才敢武装群众。比如美国，美国现在3亿人口，老百姓手里两亿五千万支枪啊・・・美国政府不担心有朝一日老百姓会拿枪打我，他不用担心，因为我这个总统是老百姓选出来的。你不是民选的政府，他绝对不敢让老百姓持枪。<br />
<br />
 <br />
<br />
袁腾飞：为什么南京大屠杀日本人敢不认账？以色列特拉维夫大屠杀纪念馆，600万犹太人，全家有的都被诛灭九族了，但是人家都有名单的记录啊。咱们这方面工作做的很差，到现在为止，南京大屠杀遇难同胞纪念馆找到了15700个名字，可你30万人，只找到了1/20啊。<br />
<br />
 <br />
<br />
袁腾飞：抗日战争时期有一个武汉会战，教材上没讲，武汉会战中国投入兵力110万，日军投入兵力35万，但是最后武汉失守继而沦陷。<br />
<br />
 <br />
<br />
袁腾飞：日本军官跟中国军官不太一样，中国军官喊的口号是：“兄弟们，给我冲”，日本军官喊的口号是：“兄弟们，跟我上”。<br />
<br />
袁腾飞：汪精卫在国民党内是元老啊，孙中山的政治遗嘱就是汪精卫起草的。<br />
<br />
 <br />
<br />
袁腾飞：为什么日本的右翼势力这么强大？它背后有很多大财团给他撑腰。这些大财团为什么给右翼势力撑腰？他们在战争期间都有不光彩的行为，比如说奴役中国的劳工，中国华北地区就丧失了600万精装劳动力，这其中还有20万被弄到日本境内做苦工，受尽凌辱虐待。如果日本承认了战争罪行，这些大财团都要赔钱呐。日本首相麻生太郎，他们家族企业就有奴役劳工的行为。你像德国企业西门子、奔驰年年都要给前纳粹劳工赔钱。<br />
<br />
 <br />
<br />
袁腾飞：抗战后期为什么蒋介石消极抗战呢？当时抗战打了一年，国军的损失超过200万，这基本上等于把抗战爆发前的家底给打光了。而八路军抗战爆发之后挺进敌后，开辟根据地，一年就从四万二发展到十五万六。所以蒋介石看再这么下去，我拼光了，天下就是你的了。<br />
<br />
 <br />
<br />
袁腾飞：百团大战不是说八路军集中一百多个团跟日军决战，要那样的话，八路军就完了。百团大战是华北地区到处开花，其实仍然是游击战。百团大战之后八路军就露馅儿了，小日本儿一看土八路狡猾狡猾的，你八路这么有实力，所以得重点进攻八路敌后战场了。于是在1941年开始对敌后根据地杀光抢光烧光。<br />
<br />
 <br />
<br />
袁腾飞：“三光”这个词不是我们说的，是当时日本华北方面军的参谋长安达二十三中将下达的作战命令。当时为了响应三光政策，日本军队连编制都改了，一个中队下辖的三个小队改为放火小队，投毒小队和抢劫小队・・・<br />
<br />
 <br />
<br />
袁腾飞：中国战争电影最可怕的地方就在于，他从来不揭示战争的残酷性，尤其是《地道战》、《地雷战》让你看完之后感觉打仗是一件很好玩的事情。<br />
<br />
 <br />
<br />
袁腾飞：有一个留日的中国作家萨苏，他看了很多日本的史料，写了一本书《国破山河在》，实际上日本人的史料里说啊，中国民兵埋的地雷根本就炸不死人，中国土地雷里面装的不是兵工厂生产的炸药，而是自产的鞭炮用的火药。小日本踩上地雷之后，一脸黑，一点事没有。<br />
<br />
 <br />
<br />
袁腾飞：毛泽东在延安发动整风运动实际上是整谁？王明。当时王明在延安处处以党的领袖自居。当时在延安抗大，王明的课很受欢迎啊，特别是女学生，因为王明很英俊，笔挺的中山装配一副眼镜，抱一堆俄文的马列经典，他对学生们说我们要学好理论，学好理论去出国苏联，学生们听完悠然神往。<br />
<br />
 <br />
<br />
袁腾飞：下一节课毛泽东来了，大家一看毛泽东那形象，头发老长都不带梳的，衣裳扣从来不系就敞着，裤腿卷着，鞋趿拉着。一堂课恨不得二三十根烟，喝完茶手就伸到茶杯里掏出来茶叶就吃了。一张嘴就是我们要到农村去，与农民结合，开荒敌后根据地，这帮学生很惊讶：这党的俩领袖怎么发出两种不同的声音啊？<br />
<br />
 <br />
<br />
袁腾飞：延安整风“反主观主义，反宗派主义，反党八股” ，招招都是冲着王明去的。<br />
<br />
 <br />
<br />
袁腾飞：延安整风不仅确立了毛泽东在党的领袖地位，更确立了他教主的地位。 <br />
<br />
 <br />
<br />
袁腾飞：二战时，按照 我们的 排列顺序：中苏美英四大盟国。<br />
<br />
 <br />
<br />
袁腾飞：二战时，美国实际上是业余时间打日本。只用半年，太平洋战争形势就逆转了。到1944年的时候，美国把小日本打的呀，沿着整个日本列岛撒了一圈水雷，这叫饥饿战役，活活困死小日本。当时日本最大的航母“信浓号”处女航刚出海，就被美国的潜艇击沉！<br />
<br />
 <br />
<br />
袁腾飞：蒋介石的中央军1944年的装备水平惨不忍睹：一个排一条毛巾，一人得红眼病，全排传染。打仗很大程度上其实打的也是后勤水平，中国人一向忽视这个，“一不怕苦，二不怕死”，你不怕死你就死去呗。<br />
<br />
 <br />
<br />
袁腾飞：打七大以后呀，中共一开会啊就是毛泽东作报告，然后大家在底下喊好，你敢不喊好吗？下次开会就没你了！<br />
<br />
 <br />
<br />
袁腾飞：毛泽东在七大做的报告是《论联合政府》，毛泽东要建立一个联合政府，跟谁联合？跟国民党联合。当时中共没有实力推翻国民党。<br />
<br />
 <br />
<br />
袁腾飞：日本军队的武器装备在二战主要国家里是最差的，当然要比中国强。他们的坦克啊，就跟咱们公共汽车的构造差不多。<br />
<br />
 <br />
<br />
袁腾飞：抗战末期，苏联红军出兵中国东北之后，在东北烧杀抢奸、无恶不作，完全是吧中国东北当战败国看待，从我们这劫掠了8亿美元的物资。后来毛泽东去苏联给斯大林祝寿拜码头，费劲巴拉要来三亿美元的贷款……<br />
<br />
 <br />
<br />
袁腾飞：小日本看咱中国的历史书，看完之后他都觉得你们这中国的历史书写的挺逗的：你们这抗日战争我们看完了，没觉得你们怎么抗日啊，净觉的你们打内战了。国共两党互相骂：共产党说国民党你消极抗日，积极反共；国民党说共产党游击就是游而不击，你七分扩张，两分摩擦，一分抗战。<br />
<br />
 <br />
<br />
袁腾飞：小日本投降的消息传到重庆，中国人乐到什么程度啊？那商店都不要钱，东西你进去随便拿；饭馆酒店开流水席，谁来谁吃，厨师炒一宿他高兴；上海外滩大楼整个从楼顶到底下一张蒋介石的巨幅彩照，整个是拿彩绸织的。<br />
<br />
 <br />
<br />
袁腾飞：我问我老师你人生经历的这些事儿里面，有一件能跟45年日本投降比吗？他想了半天，也就76年粉碎四人帮。正好这两个时间段，都是中国历史上最黑暗的时候。<br />
<br />
 <br />
<br />
袁腾飞：当年美国总统罗斯福的儿子问，为什么要援助中国呀？罗斯福说，你想，如果中国崩溃了会怎样？可以有几十个师团的日本兵从中国战场上抽调出来，他们同样会武装起500万同样是黄种人的中国人向我们进攻。他们可以占领南洋，澳大利亚，挥师印度，跟德国人会师，如果那一切发生，将是基督教文明的末日！<br />
<br />
 <br />
<br />
袁腾飞：美国在二战的时候给了英国两百亿美元，给了苏联80亿美元，给中华民国8亿六千万美元。</div>

							
							
							
							
