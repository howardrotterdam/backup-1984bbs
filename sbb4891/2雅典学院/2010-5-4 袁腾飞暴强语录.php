袁腾飞：中国现行宪法是哪一年的？82年的，中华人民共和国成立至今宪法已经换了四部，你看美国1787年宪法，两百多年至今有效，说明他的政治超稳定，我们呢？“与时俱进”！宪法是国家的根本大法，应该是万世不移的，但是我们“与时俱进”・・・ <br />
