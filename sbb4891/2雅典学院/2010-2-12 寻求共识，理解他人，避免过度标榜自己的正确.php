看了大家的回帖，我想说的第一句话就是：什么是正确？你认为正确的事情是不是要大家都认可？这句话首先就有语病，反之如果说共识就可以理解了。你来批评刘晓波的不正确，别人又来指出你的不正确，大家绞做一团，真正有共识的、可以努力的事情谁来做？谁已经做了？这个反而不去关注了。<br />
<br />
那么什么是共识，有人以身殉道；有人以暴制暴；有人认为在体制内，有人认为在体制外，有人讲究理性克制，有人号召不择手段；这些看似矛盾的背后，是不是有一种共识在支撑？我想，争取到大家的基本政治权利是我们的共识！无论是谁，在为大家的共识努力，而不是在内部争取话语权和旗帜，都是我们应该赞赏的。反之，大家看看，有多少回帖是在表现自己的正确，破坏我们对共识的努力？<br />
<br />
其实不同观点在共识的基础上是互补的，激进派保护了理性派的话语空间，理性派为激进派努力寻找出路，如果大家都认识到这些，才是社会能够真正进步的基础。激进派嘲笑理性派迂阔，理性派抨击激进派盲目冲动。有什么意义？<br />
<br />
至于lxb说的“我没有敌人”，其实很好理解。如果大家有印象的话，真正说我没有敌人是这句话：“父啊，宽恕他们吧，因为他们不知道他们做的是什么”。这句话改变了历史和文明进程，穿透了地域和种族的鸿沟。我们有“敌”，但不是“敌人”。这是很多人秉持的观念，为什么我们一定嘲笑他们、抨击他们，难道仅仅因为我们比他们更正确？</blockquote></div>对刘先生的一句“我没有敌人”的话，我认为没有争论的必要和意义，它仅仅是刘先生自己在极其特殊环境下的个人表达。我不认为一个领军人物的每一句话都是纲领性的，都有代表性。刘认为他没有敌人，不等于别人也必须认为他没有敌人。小组里有个恶习，颇具中国人劣根性的象征意义，就是一些人往往无视已有的共识，刻意地去表现自己的睿智，标榜自己的正确，就好像在争夺阵营的旗帜和话语权。<br />
我与燕归来网友在此一点上有共识：促进社会文明进步是不同类观点的人的共同目标，理论可以探讨，方向、立场问题理当坚持，但是路径、策略、方法与个人表达范围内的事，应该各执己见，避免争执。</div>

							
							
							
							
