																											<div id="postmessage_457036" class="t_msgfont">旭兄约我评《网瘾战争》，接手之时，心中有些忐忑。不是担心写不好，而是担心写过头。片中很多精彩对白，如我们只是暂住在这个国家等，若写入评论，估计无几家报纸敢发。如是，我下笔的时候，万般小心，步步谨慎，居然写了四个多小时才完稿。这种评论写多了，迟早要精神阳痿。<br />
　　见报之文仍有删节。出乎我意料的是，标题未动一字。有兴趣的朋友可以对照节本，了解一下媒体的言论尺度。<br />
　　供今日《东方早报》。<br />
　　<br />
　　<br />
　　《网瘾战争》：无权者的反讽<br />
　　<br />
　　<br />
　　这个温度起伏不定的一月，爱国青年纷纷去看《孔子》，钉子户们纷纷去看《阿凡达》，假如你是网游玩家，务必要看一看《网瘾战争》。花上一个小时，泡一壶茶，点两根烟，从一身倦怠，到热血沸腾，然后用颤动的手，轻轻拭去在眼角高傲潜伏的泪痕。<br />
　　<br />
　　也许，你已经看过了。这部以著名网络游戏“魔兽世界”为素材制作的动画电影，受众早已以百万计，并且赢得了“2010年第一部大片”的上佳口碑。“大片”一说，不啻是对动辄就耗资上亿的《孔子》式的鸿篇巨制最尖锐的反讽。据制作者言，生产这部作品，除了三个月的家用电费，没花一分钱。全凭网友群策群力，敲响了“自由的钟声”（电影片尾曲）。<br />
　　那些没有玩过“魔兽世界”的人们，不必担心看不懂《网瘾战争》。它只是借壳下蛋，电影叙事在游戏之内，批判精神则在游戏之外。如果说“网瘾”是此片的主干，那么其枝蔓横生，姹紫嫣红，诸如屁民、70码、钓鱼执法、南京彭宇案、临时性强奸、正龙拍虎、秋雨捐书等，以及长期相持不下的春哥与曾哥之争，无不被拉入电影批判的凌厉刀口，千刀万剐，体无完肤。<br />
　　由此而言，这便不仅是一部电影，还是一个历史文本：由民间力量记录、演义的活色生香的2009年中国社会生活史。这大抵奠定了此片在网络迅疾蹿红的群众基础，几乎每一个受众都能够从中寻觅到属于自己的爱与恨，欢愉与苦难，美丽与哀愁。区别仅仅在于，你是站在网瘾患者这一方，还是“杨永新”（发明了网瘾电击疗法的专家杨永信）那一方。<br />
　　<br />
　　片尾有两段声嘶力竭的激情告白，令电影的主题昭然若揭：一场无权者针对专制者的战争，更形象的说法是，只能在虚无缥缈的游戏当中找寻平等、公正之慰藉的“屁民”与电击疗法所象征的洗脑教育者的战争。观诸现实，粉红色的硝烟已然蒙蔽了神州大地，战况一直呈焦灼状态。从杨永信当选为山东省“首届未成年人保护十大杰出公民”、享受国务院政府特殊津贴，到其电击疗法被卫生部叫停，倒下的不过是他一人，背后则有无数个杨永信挺身而出，茁壮成长。尤其是《网络成瘾临床诊断标准》通过了专家论证，从此意味网瘾作为一种精神疾病而被公权力认可――当疾病贴上了明晃晃的政治标签，那么患者与医生之间的权力关系便可一目了然。<br />
　　权力的落差对应艺术的落差。电影只是对现实一幕的潦草临摹，魔兽玩家对“杨永新”的胜利更像是无权者疲软的意淫，或者说一个精神病人的梦呓。现实的荒原依然愁云密布，四面楚歌，哪怕惯于沉默的我们愤然举起了双手，却找不到希望的钟――它已被盗走了。<br />
　　<br />
　　当然，不能因此批评《网瘾战争》，批评它只是一个梦，梦醒了，伸手却不见五指；批评它给受众以希望之光，却无法指出追寻光源的路径。这是以反讽为主导的艺术作品共同的缺陷。成也反讽，败也反讽。我们更注重它成功的一面，它为什么能够走红于网络，它凭什么击中了受众心中最柔软的那一部分，唤醒他们沉睡的爱欲。<br />
　　不免还要重申时代因素。这是一个喜剧时代、大话时代；这个时代的标志性利器是幻想、反讽、顽童化、时空错位和经典戏拟；这个时代苦苦追逐的不是肃穆，而是狂欢；这个时代的无权势者所绝无仅有的武器，不是悲壮的刀光，而是滑稽的冷嘲。在《无极》与《一个馒头引发的血案》之间，时代之子更钟情后者。<br />
　　《网瘾战争》所坚守的方法论和价值观，与《一个馒头引发的血案》并无二致：无权者用反讽与戏仿的刀锋，成功解构了压迫在他们头上的多种形式的威权主义（艺术威权、话语威权，乃至政治威权）。魔兽玩家“看你妹”与“杨永新”的对决，既是无权者与专制者的对决，亦是大话话语与正谕话语的对决。不论电影内外，大话话语都是胜利者――如果冠以宏大叙事，则可说，这是个人对集体，民间对主流，自由对禁锢，开放对封闭，后现代主义对后极权主义的伟大胜利。<br />
　　<br />
　　反讽的气质呈现了时代悲欣交集的面容，反讽的理念赢得了受众雷鸣般的欢呼与鼓舞。当“杨永新”发动“核谐电击”，“看你妹”眼见不敌，便邀请电脑前的所有魔兽玩家举起手，通过局域网，给他以力量。据说，颇有一些网友看到这一幕，都情不自禁握拳、举手，甚至热泪盈眶。这一细节，包含了《网瘾战争》走红网络的全部原由。<br />
　　时代的喜剧感觉与自由观念的呼召，成就了《网瘾战争》的无限荣光；现实的苦难与残酷则是它不能承受之重。也许，正义永远虚幻，只能运行于游戏的空间与电影的世界；也许，自由的怒吼穿不透政治的防火墙，墙里，谎言老而弥坚，生活黯然神伤……然而，这些都不足以阻挡《网瘾战争》的诞生与走红。当你把这部简陋的电影看完，当你沉浸于邦・乔维的激情歌声而无法自拔，这就是一种胜利，一个人的、自由的胜利。<br />
　　因为，每一个无权者开始了自由的反讽，都将成为杨永信们的噩梦。</div>

							
							
							
							
