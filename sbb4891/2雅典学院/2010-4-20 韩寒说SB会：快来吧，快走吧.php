													<h2>韩寒说SB会：快来吧，快走吧</h2>
						
						
																											<div id="postmessage_572139" class="t_msgfont">最近，老是有媒体要关于世博会采访我，我觉得很为难，如果我赞美他吧，估计我良心不安，如果我批评他吧，估计我寝食不安。鉴于世博会将要开幕，我就在博客中集体回答关于世博会和上海的任何问题，以下便是我的态度，以后类似的问题就不要问我了。<br />
 <br />
1：你觉得世博会会给上海，给中国带来一些什么？你能用什么来比喻一下世博会呢？<br />
 <br />
答：我觉得不是世博会会给中国带来什么，而是中国会给世博会带来什么。因为本来世博会并不是一个规模如此大的展会，随着信息的流通越来越便捷，世博会正在渐渐式微，是中国将世博会升格了。如果一定要比喻的话，有点像某些在国内非常火的品牌，经过了宣传，你穿在身上觉得牛逼的不行，奢华的不行，出国一打听，原来是二线的。<br />
 <br />
2：那你如何评价世博会的吉祥物――海宝呢？<br />
 <br />
答：我认为海宝是一个让人非常头疼的东西，暂且不说海宝的形象如何，但是当时大家看到的海宝都是平面的，这给那些要将海宝立体化的人出了一个非常困难的问题，那就是海宝的背面究竟应该是怎么样的，海宝有没有尾巴，有没有屁股，有没有股沟，这些都是未知的，所以我们看到的在城市中矗立的海宝雕像，正面都是一样的，但是背面你可以发现，有的海宝没有股沟，有的海宝有股沟。但是最近以没有股沟的海宝居多，因为股沟已经宣布离开中国了。<br />
 <br />
3：世博会的会馆等结束以后就要拆除，你认为这个浪费么？<br />
 <br />
答：我认为不浪费，当时动迁，我们国家自己建设会馆，帮别的国家建设会馆，杂七杂八政府化了不少钱，这些会馆留在上海也没有用，也不能当成政府办公楼，拆了平整平整，好把这一大块地给卖了做房地产，所以到最后，其实这届世博会既不是政府开的，也不是企业开的，而是房奴和炒房客们开的。<br />
 <br />
4：那既然如此，为什么政府还要留下几个展馆不做拆除呢？<br />
 <br />
答：当然不能全拆了，全拆了这块地就不能叫“世博板块”了，到时候就卖不出一个好价钱。<br />
 <br />
5：有一些人反应世博会期间，外地车辆进上海，需要排队检查，经常拥堵几公里，排队两三个小时，你认为有更好的办法么？<br />
 <br />
答：这个我也没有办法，因为政府防贼，防恐怖分子，防反动势力和防老百姓这四防是处在同一个安全标准的，虽然我也不好说每辆车每个人都安检到底是在防谁，但是我知道如果我是坏人，我一定不会乖乖的在安检口抱着炸弹等着接受检查的。城市与城市之间的边界不像国界线那么严密，很可能相隔的就是一块稻田，有心的话很容易进出，我不认为这样的安检能检查出一些执意要干坏事的人。但也许政府觉得这样显得比较有威慑力，让坏人闻风丧胆，那大家就活该排队吧。<br />
但是无论如何，我都支持更好的安保，只要你政府估算好了得失，我愿意接受任何的安全检查。间接为了世博会，这个城市已经有不少人被工程车辆所害，所以我不希望再有任何人的生命会因此堪忧。<br />
 <br />
6：你认为世博会会为上海吸引多少的游客？<br />
 <br />
答：这个我不好说，专程为此而去和顺便到此一游是不一样的。官方说这半年里会有六千万人次来到上海，不过我估计，上海本身就是非常有吸引力的一个城市，就算没有世博会，在那半年里也会有五千九百万人次因为别的事情来此来到上海吧。反正如果我去国外旅游，这个国家正在举办不举办世博会并不对我的行程产生任何决定性的影响，可能外国人没见过什么世面，都想来看看吧。当然，我身边也有不少朋友是期待世博会的，他们都希望去看看，这些我也非常理解。世博会的场面应该会很大的，因为中国人天生都喜欢赶集，车展都有那么多人看呢。当然，我也非常能理解很多上海人希望世博会早日召开，因为如果有很多外国游客或者外地游客的话，他们可以展示给游客们看，我们上海是多么厉害的一个城市，房价五万一米，停车二十块钱一小时，油价超过一美元一升，看病吃饭坐车买东西什么都贵，生活成本是你们的五倍，工资是你们的五分之一，但我们活下来了，还欢天喜地高高兴兴的迎接五湖四海的游客。他们才是这个城市里最牛逼的展览品。我建议挑选一些市民代表作为艺术品陈列在中国馆里。<br />
 <br />
7：如何评价上海这个城市。<br />
 <br />
答：我出生在这里，我始终热爱这里，希望这个城市真正美好，虽然我的老家已经被严重的污染所占领。公正的说，如果你有钱，上海是个好地方，无论是购物，规模，消费，玩乐，上海都不错。从经济上，总体来说，这里是冒险家的乐园，这里是老百姓的地狱。<br />
但是上海是一个没有什么文化的地方。别的国家大都市，你可以说，我们这里有什么建筑，什么酒店，什么大街，什么豪宅，这些上海市的领导也可以自豪的宣布，我们这里也有，但当人家要说，我们这里有什么作家，什么导演，什么艺术家，什么艺术展，什么电影节，上海的领导就没话说了。<br />
 <br />
8：为什么会造成这种状况呢？<br />
 <br />
答：发展真正的文化就必须放开尺度，放开尺度必然百家争鸣，百家争鸣必然开启民智，这是多么糟糕的一件事情啊。<br />
 <br />
9：官方一会说转基因食品无害，一会说要严禁在世博园区里出现转基因的食品以免外国人误食，这是一种自己对自己的歧视么？<br />
 <br />
答：胡说，这明明是一种自信，是对我们中国人虎躯的一种自信，我们天天呼吸着这样的空气喝着这样的水，都是大风大浪里过来的，人家老外喝一口农药就死了，我们要喝三口才死。所以你这种说法是不对的。</div>

							
							
							
							
