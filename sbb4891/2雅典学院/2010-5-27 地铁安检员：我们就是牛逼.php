我觉得吧，发展中国家安检如此严格，并不说明它们不安全，而是因为它们从来不考虑效率的问题，一味追求安全第一。要知道，安全是有代价的，越是欠发达国家就越不考虑安全的代价。比如北京的地铁，凭什么要过X光机？有人统计过这样做的性价比吗？北京的地铁速度非常慢，开门关门的反应时间也很长，为安全留出了太多的富裕。北京几乎所有的居民大院都有栏杆，再大的院子也只开一个门，门口立一保安。这么做据说是为了防小偷，可依我看，小偷没防住，却给居民们的生活带来了很多不便。<br />
<br />
现在都在说“科学发展观”，我觉得这个概念挺好，但并不一定都体现在宏大的方面。一个国家是不是遵循科学发展观，往往体现在生活细节上。现在的中国，距离科学二字还差得很远。</div>

							
							
							
							
