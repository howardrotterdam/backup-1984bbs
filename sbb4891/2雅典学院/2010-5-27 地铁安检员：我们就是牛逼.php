<br />
这之前不知有多少次，我看到那些急着赶路上班回家的地铁乘客们在老老实实过安检的同时，安检工作人员却在忘情聊天或趴在安检屏幕前打盹，仿佛安检屏幕这玩意儿不存在。<br />
