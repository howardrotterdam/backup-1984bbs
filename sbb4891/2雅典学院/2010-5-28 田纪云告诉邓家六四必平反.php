<br />
志华说了今年1月份，曾庆红在拉美访问答凤凰台记者要为“六四”平反，其实不对。曾只说；“要有一个过程”。(杜导正《赵紫阳还说过甚麽？》P220)<br />
<br />
在 1989年六四事件中，田纪云似乎并不是一个十分突出的角色，并没有什么具有个性的非常作。人们都知道、邓小平、陈云、李先念等元老，以及江泽民、李鹏等第一线主政者当然也知道，他是赵紫阳提拔的人，但是邓小平既然说了十三大报告一个字都不许改变，那么，对赵紫阳的班底也是能不动就不动，于是田纪云就奇迹般地在靠山下台之后继续担任中共中央政治局委员、国务院副总理，而后又按部就班地退出一线，担任全国人大常委会副委员长。<br />
<br />
倒是在赵紫阳被软禁期间和去世之后，田纪云的作用才异军突起。<br />
<br />
<br />
田纪云在赵紫阳最后的时刻前往看望。<br />
