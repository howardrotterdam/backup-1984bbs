<br />
此事就拖到了2005年1月4日，田纪云通过非常管道得知赵紫阳病重被抢救过来，意识到老领导已时日无多，焦虑万分，于是，他立刻将早先准备好的信呈交给胡锦涛，没想到胡锦涛很快批准。第二天中午，中共中央办公厅回话，同意了田纪云的请求。当晚8时，田纪云前往医院，终于见到了已近15年未见过面的老领导。<br />
<br />
元月5日，田纪云见赵紫阳的时间，一共只有15分钟，这对两人而言都是多麽珍贵的机会！但是却不可能交谈任何内心深处的想法──中办秘书局的王局长，穿上白大褂大模大样地留在病房e，他的解释是“怕出问题”。<br />
<br />
这次见面留下了一张珍贵的照片，这也是赵紫阳一生中会见自己主持国政时的同事的最后一张照片。<br />
<br />
虽然那一天赵紫阳从鬼门关前又回来了，见到田纪云时情绪还特别高昂，但是关心老上级的田纪云细心观察，还是有一种不祥的预感。他悄悄提醒赵紫阳的女儿王雁南：让你们兄弟都回来吧。<br />
<br />
