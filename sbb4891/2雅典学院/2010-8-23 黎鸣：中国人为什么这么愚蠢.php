　　我在一个地方跟大家演讲，说了这么一句话：“智慧的奥秘在于语言，语言的奥秘在于概念，概念的奥秘在于范畴，范畴的奥秘在于逻辑，逻辑的奥秘在于一二三”，为什么讲逻辑一二三，因为逻辑一就是上帝，逻辑二就是西方的形式逻辑，逻辑三就是老子的三生万物。 <br />
<br />
　　大家知道我不仅仅写了《中国人为什么这么愚蠢》这本书，我还写了《西方哲学死了》，西方哲学为什么死了？因为他们把二坚持到底，他们避免不了他们的思想矛盾，他们的种种悖论，西方哲学到今天，他们取得了巨大成果，他使西方人变得比其他的人，比我们中国人更聪明，但是到了今天，这样一种二元论的形式逻辑，他所能起到的作用已经是强弩之末，所以西方哲学死了，因此，中国人将还有机会。中国人要认真地学习西方，在这里不要有任何的自大，我们没有任何资本，我们已经愚蠢了两千多年，现在还在愚蠢。我现在希望在座的年轻朋友们，不要以为我这老头子在这儿发疯，我说的是实话，是实话实说，你们应该有志气，使自己聪明起来，现在是一个机会。西方人比我们强，但不是绝对的强，只是相对的强，只是因为我们没有为自己创造出有利的条件。今天这个机会正在向我们走来，但同时向我们走来的也有危机。 <br />
<br />
　　刚才开场白我就说了，我们今天离整个世界的各个方面的平均值不是越来越靠近，而是越来越遥远，这是非常可怕的事情，让这样一种趋势始终在我们这个世纪以及下一个世纪照样下去的话，我们中国人一定会成为下一个印第安人。人对人是非常残忍的，不要把人对人看得是多么仁慈，没有那么仁慈，西方人从来就没有很仁慈地对待过我们中国人，至于旁边的日本人，就更加不仁慈，何止不仁慈，简直残忍之极。中国的年轻人永远不能忘记了日本人对中国人的最残忍的屠sha，这场屠sha很有可能是还会到来的最后的更大的屠sha的预演。我们的年轻人不要对于任何人保有任何的幻想。只有让我们使自己聪明起来，强大起来，我们才能为我们自己、为我们的子孙留下一片生存的空间。 <br />
<br />
　　我们千万不要被眼前的所谓轻松的生活、歌舞升平迷走了眼神，没有歌舞升平，那都是虚的，那都是假的，别浪费了我们的时间，尤其是你们年轻人的宝贵的时间。应该振作起来，现在是机会，我现在告诉大家，我们从哪儿来开始，就从哲学开始，虽然我说西方哲学死亡了，但是西方哲学在近三百年，他的的确确使西方人变得比我们聪明。我们现在必须把西方的那种真正能使人聪明的哲学赶紧拿到我们中间来，我刚才告诉大家，我们过去没哲学，两汉经学是伪神学，宋明理学是伪哲学。而近50年我们所宣扬的所谓马列主义哲学，同样是妨害人们正确思维的哲学，是对于人的思考没有什么价值的哲学，继续让这样的哲学在我们的大学毒害我们的年轻人，中华民族就真是没有希望了。不要去读所谓的马克思的哲学，马克思本人根本就不是什么哲学家，马克思是一位杰出的社会学家或经济学家，但他不是哲学家，他关于思维所走的路是非常偏颇的。从马克思到列宁，说实在的，列宁是一个更糟糕的人，当他主张唯物主义，把唯物主义当做唯一的流派的时候，他就已经把哲学弄死了。我希望大家认真听取我的意见，我对大家是非常真诚的，我看了那么多的哲学书，不要被任何人的名头所吓到，什么海德格尔，什么维特根斯坦，什么胡塞尔，等等，他们固然有自己的成就，但是在康德面前，他们都只不过是一个小小的土堆，康德才真正是一座大山，是真正值得我们去挖掘的一个伟大的智慧之源。 <br />
<br />
　　爱因斯坦为什么那么聪明，其中有一条我认为是很重要的，爱因斯坦十三岁就通读了康德的纯粹理性批判，什么叫做纯粹理性批判？纯粹理性批判解决的就是一个问题，什么样的知识是真正有价值的知识，而什么样的知识是假知识，是伪知识，是根本不要去学它的知识，就是这么一个问题。正是因为这样，爱因斯坦也给后来的哲学家留下了大片的具有活力的、研究的空间。 <br />
<br />
　　康德在我的眼睛里头是最伟大的哲学家。雅士贝尔士也跟我有同样的见解，他认为西方只有三个哲学家才是真正对整个人类立下了不朽的功勋的人，他所指的三个人是柏拉图、奥古斯丁和康德。而在我看来，奥古斯丁固然伟大，但是比之柏拉图，比之康德还差一点，所以在我的眼睛里头，只有十二个人，这十二个人前面我已经说了。这十二个人是永远不朽的人，他们是应该永远活在我们心里的人，东方四个，中国三个我刚才已经说了，印度的佛陀释迦牟尼，他也是一个伟大的哲学家。西方八个，我也说了，古希腊三个，犹太人两个，以及英国人一个，法国人一个，德国人一个，而德国人的康德可以说他把所有西方八个人的成就都奠基在他的书里头，他的三种理性批判，简直就是整个人类未来的永远不会枯竭的智慧之源。 <br />
<br />
　　我希望在座的朋友们，如果你们没有看他的那三部书，你们就应认为是自己的失误，认真地去读他一种，看看我黎鸣是不是骗你。当你真正认真地把康德的纯粹理性批判读一遍，而且真是读懂了，你会觉得你以前读的哲学书都是垃圾，大部分都是毫不值得一读的东西。中国有一句老话叫打蛇打七寸，知识是永远掌握不完的，世界上的书你永远读不完，但是你一定要读最好的书，最能使你聪明的书。我可以这么说，图书大厦，90％以上是垃圾。我们的报纸，我们的刊物，那些文章95％是垃圾，不值得去读。至于网上，这样的垃圾更多。 <br />
<br />
　　人的生命是有限的，你应该在你尽可能短的时间里，让你自己变得聪明起来，这是绝对能做到的。我认真地考量了整个德国民族的崛起，德国人在19世纪，实际比英国，比法国落后多了，然而，就是因为他们把新康德主义贯彻进了他们的教育系统，不到三代，仅仅就是三代人，一代人算二十年，三代六十年，就是在那六十年之中，德国人在整个自然科学、社会科学、心理科学突然地崛起，说实在的，我们现在都在享受人家智慧的成果，我们应该认真地去学习康德。 <br />
<br />
　　刚才讲了爱因斯坦，爱因斯坦为什么那么聪明，康德对他的影响不可小看，因为他抓住了思维的最重要的关键和方法。中国有句老话，“工欲善其事，必先利其器”，只有你真正掌握了最好的工具，你才能把你的工作做得最好，而只有你掌握了最好的思维的工具，你才能使自己真正变得聪明起来。而现在就是机会，因为我在我的《西方哲学死了》的书里头，已经预言了西方人，他们也正在不可抑制地不自觉地变得愚蠢起来，因为他们的哲学也完蛋了。什么后现代主义？我们的一些中国文人把所谓的后现代主义哲学家捧得那么高，自己却一个屁都放不出来，还号称是什么中国的思想家。说实在的，这非常可悲。我没那么多时间跟他们辩论，但是只要可能，无论到北大到清华我还是愿意跟他们辩论，我曾经向北大的孔庆东发出挑战，跟他辩论关于金庸的小说到底是对中国青年有益还是有害。 <br />
<br />
　　刚刚有报道说，要把《天龙八部》当做高二的语文读本，这太悲哀了，这个民族实在太悲哀了。金庸给我们的年轻人到底是一种什么样的思维方式，是一种什么样的思维观念？侠义，侠义是什么东西，侠义是专制逼出来的。当你在那儿宣扬侠义的时候，你同时也就宣扬了专制，而他运用的想象力跟**功有什么不同，我们这里反对**功，而在那里却又把同样宣扬跟**功差不多的东西的人当做中国最伟大的文学家，他那叫什么功，他那叫吹功，他一掌打翻一座山，这是什么思维，这种思维能产生有价值的东西吗？这种东西在康德的眼里是不折不扣的垃圾。而我们的年轻人居然不知道什么是真的什么是假的，什么是善的什么是恶的，连最起码的判断力都没有。而有罪者是谁，是北大的教授。你孔庆东做了最无聊的判断，人家认为你是最高学府的权威，而你这个权威做了什么判断，做了最愚蠢的判断。 <br />
<br />
　　很可悲，我就没有可能到你们北大跟他辩论，你们北大的校长敢请我么？如果敢，我一定到你们北大跟孔庆东先生当面辩论，我们辩一辩，究竟谁在宣扬智慧，谁在宣扬愚蠢。中国的文人太悲哀了。我曾经说过几句话让文人不高兴，中国文人是非常卑鄙的，卑鄙在哪儿？卑鄙在没人格，中国的文人连最起码正确的思维方式都没掌握就在那儿高谈阔论，中国文人的思维是反动的，为什么是反动的？因为他们写的文章，他们写的书，要继续使我们中华民族的青少年永远聪明不起来，认识不到这一点。而仅靠我这一张嘴根本没有用，就算拼命也没有用。 <br />
<br />
　　非常感谢“第三极”能请我到这儿来，跟在座的朋友们进行思想交流，其实我更愿意到北大的礼堂，到清华的礼堂，我去跟那些所谓的教授们叫板。这决不是为了我个人，当然，我不是圣人，我说的话也决不是100％的都正确，但是我愿意辩论，因为只有在辩论的过程中，才有可能突现真理，至于谁胜谁负，那并不重要，也没关系，但是问题需要辩论。什么叫辩证法，辩证法并不是像我们在课堂里所学的那一套教条，辩证法是辩论法，一个没有辩论的国家他有什么资格去宣扬辩证法呢？真正把辩证法看得最清楚的是康德。康德的四对二律背反，真是把问题的本质给提出来了，而绝对不是黑格尔，仅仅简简单单的三条规律，什么对立统一规律，什么量变质变规律，什么否定之否定规律，认真地说，这三条根本说不上是什么规律。你认为黑白规律是规律吗？如果黑白不是规律，你也就能否定上述的三条是规律。我跟你说，他完全把康德的思辨精神给玷污了。当康德在讲一二三的时候，黑格尔讲什么，黑格尔讲正反合，然而，正反和依然是二元论，可是康德不完全是二元论，康德有一句话，他曾直截了当地指出，三就是完备，三就是全。这很重要，这说明了康德对三的奥秘他看得很清楚，而黑格尔则完全歪曲了康德的精神。把一二三变成了正反合，所谓“第三极”说得好，不是合，是第三极，三者，三足鼎立也，这才是真理。 <br />
<br />
　　我今天的演讲时间有限，更多的是我想给在座的朋友们心里点起一把火，让这把火烧起来，还要烧到你的同学们中去，一定要让自己振奋起来，让自己变聪明，而这不是神话，是有踏踏实实的路给你走的，是有前人给你铺的阶梯帮你迈上去的，这个阶梯最近的就是笛卡尔、休谟、康德，其他的哲学家说得天花乱坠，不要去看他们，他们已经变形了，他们把哲学里边真正有价值的东西给抽没了，他们所辩论的东西都是细枝末节，只有康德把哲学最本质的东西变成了三句话。 <br />
<br />
　　第一句话，我能知道什么。第二句话，我应该做什么。第三句话，我可能希望什么。这三句话在纯粹理性批判的后面提出来了，这三句话其实就是所有的哲学都必须认真地回答的问题。任何一个哲学家把这三个问题抛到一边去讨论其他的东西，那他就没有资格叫哲学家。所以我希望在座的朋友们，把我刚才复述的康德的三句话牢牢记住。我再讲一遍，康德说，所有的哲学，无非都在于帮助每个人对自己提出如下的三个问题。第一个问题，我能够知道什么。第二个问题，我应该做什么。第三个问题，我可能希望什么。康德用他的三个批判来回答这三个问题。第一本书【纯粹理性批判】就是回答第一个问题。我能够知道什么？第二本书【实践理性批判】，他就回答第二个问题，我应该做什么，第三本书【判断力批判】，就在回答第三个问题，我希望什么。你看康德的思想既清楚又明白，又抓得非常准，就是因为这样，他的哲学为整个德语民族，当然也是为整个人类留下了一汪永远不会枯竭的智慧的源泉，谢谢大家。</div>

							
							
							
							
