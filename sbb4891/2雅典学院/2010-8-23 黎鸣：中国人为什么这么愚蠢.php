　　第一个，中国人不讲平等；第二个，中国的统治者不允许你聪明，总想用愚民方式搞极权专制；第三个，你的思维方法有问题。你连怎么思考都不懂，你怎么去变得聪明？你们在北大在清华没有学到真正思维的奥秘，因为你们的导师都是糊涂蛋。五十多年了，我们把黑格尔、把马克思当做最伟大的哲学家，错了，真是错了。刚才我讲，西方近代的三个最伟大的哲学家，第一个笛卡儿，第二个休谟，第三个康德。从康德到黑格尔是一个巨大的倒退。康德的哲学构成了整个德语民族成为了世界上最聪明的民族的最关键的基础，英国人根本就比不上德国人，大家看看整个的科学领域，自然科学、社会科学、心理科学，那些最重要的理论是谁首先发明的？德国人，德语民族。现在我们认为物理学中最伟大的两个理论，量子力学、相对论，不是英国民族提出来的，是德语民族提出来的，为什么？那是因为他们具有最强大的哲学思维的力量。康德的哲学武装了整个德语民族，康德的哲学使得德语这种语言变成了世界上最能使人聪明的语言。 <br />
