唯一值得骄傲的就是漫长的历史，也就是一个5千年数字本身，但是悖论和可悲的是，在这么漫长的历史中，却从来都是一遍 ... </blockquote></div>我相信外国的东西我看的不比你少，胡适早期可是犯了不少错误的，不过好歹他学的毕竟是外国的精髓，总算没一条道走到黑，很多东西是殊途同归的，无非是讲法不同罢了，看看胡适后期写的文章吧，就我看来，顺眼多了。<br />
以下摘自胡适1954年的一次演讲：<br />
《胡适的声音》中之《中国古代思想史的一个看法---1954台大演讲》<br />
<br />
。。。。。。<br />
第二件事，是孔子、孟子一班人提倡的一种自由主义的教育哲学。孔子与孟子首先揭橥这种运动。后世所谓道家，也可以说是这个自由主义运动的一部分。后来的庄子、扬朱都是承袭这种学说的。这种所谓个人主义、自由主义的教育哲学和个人主义的起来，是由于他们把人看得特别重，认为个人有个人的尊严。《论语》中的“不降其志，不辱其身”，就是这个道理。个人主义、自由主义的教育哲学，教育人参加政治，参加社会；这种人要有一种人格的尊严，要自已感到自已有一种使命，不能随便忽略他自已。这个人主义、自由主义的教育哲学是第二件值得记念的大事。<br />
。。。。。。<br />
第二件大事是孔子以下的自由思想、个人主义。。。。。。他提出的教育哲学可以说是民主、自由的教育哲学，将人看做是平等的。《论语》中有：“性相近也，习相远也，唯上智与下愚不移。”就是说，除了绝顶聪明与绝顶笨的人没法教育以外，其它人都是平等的，可教育的能力一样。孔子提出四个字，可以说是中国民主主义教育哲学，就是“有教无类”。。。。。“类”就是阶级，有了教育就没有种类，就没有阶级。后世的考试制度，可以说是以这种教育哲学为背景的。<br />
孔子的教育哲学是“有教无类”，但他的教育教什么呢？孔子提出一个很重要的字，就是“仁”字。孔了的着重“仁”字，可以说是前无古人后无来者。这是了不起的地方。这个“仁”就是人的人格、人的人性、人的尊严。孔子说：“修已以敬。”孔子的学生又问：“这就够了吗？”孔子又说：“修已以安百姓。”这句话就是说教育并不是要你去做和尚，去打坐念经那一套。“修已”是做教育自已的工作，但是还有一个社会目标，就是“安人”。“安人”是给人类以和平、快乐。这一个教育观念是新的。教育并不是为自已，不是为使自已忧为菩萨、罗汉、神仙。修已是为了教育自已，为的社会目标。所以，后来儒家的书《大学》里的“格物，致知，诚意，正心，修身”，是亻身的工作，而后面的“齐家，治国，平天下”，都是社会的目标。所以孔子时代的这种“修已以安人”、“修已以安百姓”的观念就是将教育个人和社会贯连起来。教育的目标不是为自已自私自利，不是为升官发财，而是为：“安人”、“安百姓”，为“齐家”、“治国”、“平天下”。因为有这种使命，就感觉到“仁”-------受教育的“人”，尤其是士大夫阶级，格外有一种尊严。人本来有人的尊严，到了作到自已感觉有“修已以安人”，“修已以安百姓”的使命时，就格外感觉到有一种责任。所以，《论语》中说：“志士仁人，无求生以害仁，有杀身以成仁。”就是说，遇必要时，宁可杀身以完成人格。这就是《论语》中的“不降其志，不辱其身”。孔子的大弟子曾子说：“士不可以不弘毅，任重而道远。仁以为已任，不亦重乎？死而后已，不亦远乎？“就是说受教育的人要有大气魄，要有毅力。为什么呢？因为”任重而道远。“任”就是担子，把“仁”拿来做担子，担子自然很重。到死才算是完了，这个路程还不远吗？这一个观念，是我们所谓孔孟学派的精神：就是将个人人格看得很重要，要自已挑起担子来，“修已以安人”、“修已以安百姓”。孟子常说：“自任以天下之重。”曾子说：“仁以为已任。”以整个人类视为我们的担子，这是两千五百年来一个了不得的传统。后来，宋朝的范仲淹也说：“先天下之忧而忧，后天下之乐而乐。”这就是“修已以安人”而感觉到“任重而道远的缘故。明未顾亭林以为“天下兴亡，匹夫有责”，也是这个道理。<br />
所以，自由民主的教育哲学产生了健全的个人主义。个人主义就是将自已看作一个有担子的人，不要忘了自已有使命，有责任。不但孔子如此，孟子也讲得很清楚：“富贵不能淫，贫贱不能移，威武不能屈；此这谓大丈夫。”就是说大丈夫的人格要自已感觉到自已有“修已以安人”的使命。。。。。。以健全的民主自由教育哲学作基础，要做到“不降其志，不辱其身”；提倡人格，要挑得起人类的担子，挑得起天下的担子。宁可“杀身以成仁”，不可“求生以害仁”。这个健全的个人主义，是第二个重要运动。<br />
