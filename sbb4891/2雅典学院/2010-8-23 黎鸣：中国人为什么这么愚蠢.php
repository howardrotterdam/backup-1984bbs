时间：2005年3月5日下午2：30分 <br />
<br />
　　地点：海淀图书城昊海楼第9层――“第三极”演讲 <br />
<br />
　　 <br />
　　主讲人：黎鸣 <br />
<br />
<br />
<br />
黎鸣：同志们好，今天到这里来，是填补我一个空白。刚才讲了，我就这个题目，从去年的10月到12月走遍了全国，基本上是西到兰州，西安、东到上海，南到深圳，北到长春，一共在39个大学作了演讲。后来因为学校要考试，放寒假，所以再走下去也不行了，只有在年底回家。然而在北京，一个大学都没讲，今天在这儿听讲的有很多是北大的同学，这也算是填补了一个空白。本来我是希望在北京多讲的，但是讲不了，因为我这个话题比较敏感。中国人愚蠢，中国人的确是愚蠢，不是一般的愚蠢，而是非常的愚蠢，愚蠢到自己在未来的一百年到二百年之中将很可能会使中国人自己变成第二个印第安人。中国人的愚蠢不是一般的愚蠢，我希望中国的年轻人能警觉到这个问题，不要以为我们现在中国人有十三亿十四亿，到了一定的时候，首先会被屠sha的就是那种没有智慧的民族。 <br />
<br />
　　世界的人口在大量地增长，刚刚不久联合国发布了一个关于生态问题的报告，这个报告表明，我们现在的地球人正在寅吃卯粮，我们正在把我们子子孙孙应该有的生存资料也给糟蹋了。在这份报告中，提出了一个概念，叫做生态足迹。美国人的生态足迹平均是9.2公顷，中国人是1.5公顷，而中国人真正能够提供的只能是0.8公顷。也就是说，我们其中有0.7公顷已经在吃子孙了，而世界的平均足迹是1.8公顷，也就是说我们现在消耗的还没有达到世界的平均值，所以这是一个非常严峻的事实。生态问题是非常严峻的。 <br />
<br />
　　刚刚报道，在海南50万人没水喝，为什么？干旱。总有一天偌大的北京1500万人，突然有一天告诉你，明天就断水了，这是什么消息？整个北京，将一下子变成一座死城，将立即完蛋，这不叫危言耸听，这将很可能有一天会变成事实。中国人现在还在那儿自我表扬，自吹自擂，有什么资格自吹自擂呢？现在我们自认为我们的经济已经非常进步了，各方面都在和平地崛起，但是事实上我告诉大家，我们几乎一切的方面，离开世界的平均值不是拉近了，而是越来越倒退了，我们现在的科学水平离世界平均水平的距离，还不如过去的毛泽东时代。 <br />
<br />
　　我们现在的发明能力，创造能力，比之50年代还要差得远。去年发布了一个消息，世界上关于发明创造的专利，中国人所提交的占世界整个不到1％，而且都是低级水平。我们现在经济这么发展，发展的结果是如何呢？我告诉大家，我们去年九点几的GDP增长，其中7％靠的是什么？靠的是进出口，而进出口之中，我们在其中拿到的利润是多少？17％。也就是说83％装进了外国投资者的口袋，为什么？因为我们提供的劳动是低级劳动。 <br />
<br />
　　比如说IBM电脑，在中国有多条IBM生产线，生产出来的电脑作为美国产品出口，其中我们为它干了什么？机壳、按键、开关，关键的部件、设计、重要的软件由美国提供。所以我们只能从中拿到那么一点点的比例，17％。你看我们的GDP增长了，但是我们中国人的人均收入并没增长。这样一种经济发展对我们中国到底带来了什么，而我们的生态破坏在世界上却是最严重的。 <br />
<br />
　　世界上十个最污染的城市有的讲九个在中国，有的甚至讲十个全在中国。昨天参考消息有一篇报道，中国已经变成了发达国家的垃圾场。人家的废钢材、废塑料，破铜烂铁全都拥进了中国，中国变成了别人的垃圾场。在广东、在福建很多的农户就用自己的锅把废电脑的接线板往里扔，用这样一种操作来炼制其中的某些元素，就用这种方式来挣一点钱，来养活自己。结果如何？结果那个村庄的水不能喝了。我们就是过这样的经济发展生活，这样的经济发展下去，我们中国人还想活不想活了？我们是靠什么去发展经济？我们是靠我们可怜的手，我们高贵的大脑却一点也没有用。中国人根本就不会思维，不光是一般老百姓，就是北大的清华的那些教授们，以及那些以为自己多么了不起的博导们、研究员们，他们会思维吗，他们用一种什么方法教导我们的年轻人？ <br />
<br />
　　在座的北大同学，我相信你们是非常优秀的，有句话，北大是一流的学生，二流的教师，三流的管理，我认为，北大大概就是这样的状态。把我们最优秀的学生送进这个学校，培养出来的人并不见得优秀，并不是这些学生不优秀，他们没辙，他们受的教育不可能使他们优秀。 <br />
<br />
　　中国人的愚蠢并不是从今天开始，从秦汉以后中国就是一个漫长的愚蠢的过程，就是一个愚蠢的民族，只不过在公元15世纪之前跟西方比较，还不是那么严重，因为西方人有自己的问题，大家都只能彼此彼此。但是在近300年，西方人已经变得越来越聪明，我们依然是那么愚蠢。我们根本就不会思维，为什么不会思维？第一不允许思维，第二必须像某某一样思维，第三你不能质疑，你只能像蚂蚁一样活着。 <br />
<br />
　　所以毛泽东有时候作为一个诗人还不错，他曾经说过一句话，一句诗，叫做“蚂蚁缘槐夸大国”，中国人就是“蚂蚁缘槐夸大国”。我们凭什么叫世界大国，真正的世界大国是智慧大国，是聪明大国，是发现、发明、创造的大国，而我们中国人为世界发现了什么，发明了什么，创造了什么？而所有这一切靠什么？靠脑袋，靠脑袋的思维。一个不会思维的民族，他们怎么去发现，怎么去发明，怎么去创造呢？一直到今天，而且现在，我们不是在前进，我们反而在倒退，我们必须认识到目前这种形势的严重性。时间留给我们的已经不多了，现在全地球60多亿人口已经是非常严重了。 <br />
<br />
