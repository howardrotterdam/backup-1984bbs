　　F―M89是现代人中占压倒性的类型，大概全世界80%以上的人都有这个变异点。　 <br />
　　F―M89有很多子类型，其中，K―M9是最重要的一个支系，亚欧大陆上，除了西亚地区和西伯利亚，世界岛上绝大部分民族都由K―M9的子孙构成，今天中国人（汉族）中，96%的人都是K―M9类型。　　 <br />
　　K―M9下面也有很多子类型，最重要的有两个，一个是ON―M214，另一个是P―M45。<br />
　　前者（ON―M214）主要分布在东亚、东南亚、北亚东部、部分太平洋岛屿（以上都是O―M175子类型）和乌拉尔山两侧、北欧北部、东欧北部和北极圈内的爱斯基摩人（以下都是N―M231子类型）。中国人主体类型就是O―M175。<br />
　　 <br />
　　后者（P―M45）主要分布在欧洲大部（R―M173子类型）、西亚的伊朗、南亚的印度（R1a―M17子类型）、美洲印地安人（Q―P36子类型）等地区。其中，R―M173（包括M17），被认为是原始雅利安人的基因。<br />
推测的他们迁徙的路线<br />
D-YAP矮黑人基因 <br />
M130棕种人基因 <br />
汉族人无论是南方汉人还是北方汉人,Y染色体中都没有这两种基因 <br />
而北方民族蒙古人,突厥人,满族人,朝鲜人和日本人都有D-YAP和M130 <br />
汉族人的基因是96%的M9+4%的M45,其血统纯度在世界大民族中首屈一指 <br />
其中K-M9是黄种人基因,是中国人的主体类型,P-M45是白种人基因 <br />
C-M130是棕种人基因,海洋居民，C―M130走的路线大概就是沿着海岸出发，从阿拉伯半岛―伊朗―印度―中南半岛，此后，这批M130的子孙分为两支系，NO.1支系向北进入西伯利亚，并最终进入了北美地区；NO.2支系向南进入澳大利亚，并扩散到整个太平洋诸岛。虽然我们的基因树上C―M130画的比较简单，但实际上C―M130并不单一，他们类型众多。 <br />
　　 <br />
　　 <br />
　　今天C―M130的后代主要分布在印度南部、北亚东部、日本、北美西部、澳大利亚、太平洋诸岛。在东亚地区，外蒙地区比例非常高，达到58%，还有哈萨克人、吉尔吉斯人、布里亚特人、比例都在60%以上，这些人群的类型（包括北美地区）都是C3―M217；M130的其他类型还有，日本的阿伊努人90%以上是C1―M8，另外，现代日本人（6.5%）、朝鲜人、满州人、阿穆尔人中，也有一定数量的C1。C2―M38，主要分布在澳大利亚和太平洋诸岛屿，C4、C5主要分布在南印度地区，在当地民族中能达到20%。 <br />
在汉族人中几乎没有C-M130基因的分布 <br />
D―YAP又被称为小黑矮人基因，是一支非常古老的基因，它和C―M130几乎同时走出非洲。现在D―YAP类型主要分布在印度安答曼群岛的安达曼人（100%）、藏族（58%）、土家族、彝族、瑶族、日本（34.7%）、朝鲜、满州人、缅甸人、克钦人。在汉族人中，D―YAP在汉族人群中几乎没有发现 <br />
　Y染色体变异数量多少并不能用人口数量来衡量，很多人口众多的大民族都是由一个小人群由于某种原因迅速壮大而来的，如历史上曾建立了强大帝国（典型的是汉人、阿拉伯人），近代的高技术革命和殖民（如英格兰人、西班牙人），这些人群的同源性强，Y染色体的变异类型并不特别丰富，相反，一些非常古老的小民族，其Y染色体变异类型却十分丰富。 　　 <br />
