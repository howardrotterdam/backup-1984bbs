																											<div id="postmessage_808964" class="t_msgfont">　　南北朝时期长达垂三百年，五胡乱华占据了华夏的十分之九，华人的社会地位低于胡人。华人皇后所生的太子都不被胡人上层社会所接受，被掳的汉人女子更是不计其数。北齐强迫汉人妇女嫁给胡人将士，有夫之妇被抢走的占十之二三。南朝与北朝也进行通婚，不存在什么华夷之分，南北朝末期胡人的数量不少于华人的数量。魏国化夷为夏，原有的九十九种姓都改为汉人姓氏，北周又恢复了原有的姓氏，连姓氏都混乱了。由于战乱，大量的汉人与胡人流落到朝鲜半岛与东南亚地区，生活习俗也随顺了当地。华夏入蛮夷则为蛮夷，蛮夷入华夏则为华夏，是没有一定的。华人过去指的是农耕民族，五胡乱华之时胡人也进入了农耕社会。北周仿效周朝实行井田制，每丁分田八十亩，妇女四十亩。<br />
<br />
　　鲜卑成为华夏社会的主导力量，朝鲜民族为东夷，隶属于鲜卑政权，辽东近百个诸侯国大多为鲜卑，三韩与鲜卑其实无法分清的。鲜卑入主华夏之后，鲜卑与汉人也是无法分得清的。许多汉人随顺了鲜卑的姓，觉得是个荣耀。隋唐的君主都出身于鲜卑，文武百官的民族也很难分得清楚，也没人在意那些问题。华夏是天下的中心，古人没有民族与国家的概念，只有地理区域概念，没人刻意保留民族习俗。<br />
<br />
　　南北朝之后纯粹的汉人有可能存在么？这是个很难回答的问题。汉人政权龟缩于东南一隅，只占华夏的九分之一。侯景之乱又被胡人搅的一团糟，妇女都作为战利品被胡人掳入军营，纯粹的汉人能在哪里？游牧民族没有本民族文化，接受汉文化是他们最佳的选择，也是唯一的选择。胡人政权大多被汉化了，连极权等级制都全盘接受，上层社会形成新的门阀世家，通婚讲究门当户对，华夏上层社会的奢侈腐败也为新兴贵族所接受。南朝已经走向衰亡，上层社会还是骄奢淫逸，顿餐数万钱，食一看百，朱门酒肉臭。百姓饿的人食人，粮食都被强征了去，没人管百姓的死活，无人愿意种地。战争的成果是掠夺人口，强迫掠来的人口进行耕作，属于奴隶的身份。五胡乱华时期人口的流动性最大，十室九空，下层社会是无法决定自己的命运的。<br />
<br />
　　<strong>长达二三百年的衰亡足以使早期的汉民族不复存在</strong>，高丽族也不复存在，百越人也不复存在，连纯粹的鲜卑人也不复存在。南北朝时期是民族大融合时期，种族与血缘杂交时期，只有生活于高原地区的人类才有可能较多的保留着原始状态，这也是崇尚种族主义的纳粹到西藏寻找纯粹雅利安人的原因。人类社会的发展进程与人口的流动与迁徙是分不开的，种族之间的杂交是不可避免的，否则将会导致退化，早期社会同姓之间是严禁通婚的。</div>

							
							
							
							
