<br />
　　在《太平天国奉天讨胡檄》里有这么一段话：“中国有中国之人伦，前窝康熙，暗令鞑子一人管十家，淫乱中国之女子，是欲中国之人尽为胡种也。中国有中国之配偶，今满洲妖魔，悉收中国之美姬，为奴为妾，三千粉黛，皆为羯狗所污；百万红，竟与骚狐同寝，言之恸心，谈之污舌，是尽中国之女子而玷辱之也。”&nbsp;&nbsp;<br />
