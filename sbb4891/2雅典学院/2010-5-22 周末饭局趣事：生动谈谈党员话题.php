																											<div id="postmessage_631284" class="t_msgfont">周四友人打电话来约周五晚间饭局。对此类酒肉交往本不想应约，因为近来傍晚与妻携狗散步成瘾，奔赴酒会必舍弃与狗同乐。可朋友死皮赖脸强拉硬扯，不答应朋友实在有悖世风，用市井话语来说就是“你还想不想混了”，于是有了下面的有趣经历。<br />
<br />
饭席设在依山傍水处，酒桌上的人大多相熟，不过也有二三个生面孔。有趣的是我的座位置于两位意识形态官员之间：一位是当地意识形态主管，另一位是女士，说来我们应该是老朋友，不过好多年未见，她已升至党校高层。由于此后多谈及此女士，我将其简称为R女士吧。<br />
<br />
酒宴前戏必然老套，无非相互奉承，外加询问未到场的老友近况。两位宣传主管置我左右给我的交际实战增加了巨大工作量，好在意识形态主管经常能见到，不用多客套，冷落女士却是要被骂缺乏绅士风度的。于是我卖力地夸赞R女士越来越漂亮，时间的刀剑见到这样美的脸蛋也却步，未敢留下一丝一毫的痕迹云云。我说的都是实话，近读沙叶新的“底线是不撒谎”一文感慨良多，赴此饭席时余波尤在，底线并未置于脑后。<br />
<br />
见我如此夸奖，其他人也插话进来，向我介绍他们听过R女士在大学巡讲“三个代表”，水平如何如何高。听过大家的介绍，我的兴趣逐渐地浓了起来。当然越来越感兴趣的不是脸蛋，而是她宣讲的内容。<br />
<br />
我向她凑近了一些，问：“你对你讲的内容相信吗？”<br />
<br />
她笑了笑说：“当然相信。”<br />
<br />
我又问：“你对三个代表如何理解？”<br />
<br />
她说：“你看，我到处讲演三个代表，宣讲的就是我的理解。你说说你对三个代表如何理解吧。”<br />
<br />
我这个人城府浅，心里有一点水就流出来，孔子“敏于事慎于言”的教诲虽然滚瓜烂熟，可就是一次也没真正做到过。既然各大学巡讲三个代表的R女士想听我的看法，我自然要抓住这个难得的机会。因为大家知道，在中国的主席台和听众席之间隔着深沟大壑，是天地之间的距离，听众只有听的份，哪有谈谈自己理解的机会？现在天地之间的距离突然拉近了，仙女下凡要体察民意，我一定要知无不言。于是我把我对三个代表的理解，比如向社会党性质转化，有全民党的意图，赞成其抓大放小，国退民进、股份制转型政策、加入世贸、江时期公民社会开始形成等等悉数一遍，大多是表示赞成，并例举在上海APEC会议上，江core用上海话讲的一锹掘不出一口井来表示江的渐进改革意图。<br />
<br />
我讲这些话时R女士一直听得很认真，频频点头，并夸我对三个代表理解得很深。讲的是三个代表，如果就此打住不论其后必然是皆大欢喜，饭桌上一定呈现出一派和谐的大好局面。可惜我的话匣子一打开就关不上了，话锋一转，我说02年后经济政策是国进民退，公民社会萎缩，就偏离了三个代表的初衷。我接着说，老百姓理解的和谐和高层说的和谐并不是一回事。老百姓理解的和谐是安居乐业，上情下达，心情舒畅，社会体现公平正义；高层理解的和谐却是没有批评的声音，社会平静，政权稳定。这种理解上的偏差往往造成了民众心情压抑，社会反而动荡不安。<br />
<br />
R女士说:“你要相信，作为执政党，高层考虑问题肯定更全面，信息掌握得也更充分，中国问题很复杂，不像你想得那样简单”。我感觉官腔出来了，那就一定要往具体处拉，当然我有言在先是纯学术讨论，避免涉及具体社会事件。<br />
<br />
我问：“高层考虑问题的出发点是什么？”<br />
<br />
R女士：“党的利益和前途，民族的利益和前途”。<br />
<br />
我说：“高层将党的利益和前途置于民族利益和前途之上了，这从最近戴秉国的讲话可以证明。”<br />
<br />
R女士回道：“党的利益和前途是与民族利益和前途一致的，你不能将他们人为割裂开。”<br />
<br />
我说：“党的利益和前途与民族利益和前途是两件事，是完全分立的，两个分立的事可能方向一致，也可能方向部分一致，构成张力，也可能方向完全相反，构成反力。说党的利益和前途与民族利益和前途完全一致在逻辑上不成立，实践上我也能找到反例。”<br />
<br />
R女士让我拿出一个反例，我毫不费力地举出文革。我说，文革是一场浩劫，这是有我党决议的，至少在文革期间我党的利益与民族利益是不一致的。<br />
<br />
我又发问：“R能否说说我国现在的国家性质，是不是无产阶级专政的国家？”，我沉不住气，没等R女士回答就自问自答地说肯定不是无产阶级专政的国家，那么是什么国家性质？<br />
<br />
R女士回答说：“人民民主专政的国家性质”。<br />
<br />
我说，能不能具体说说什么是人民民主专政，这里的人民指哪些人。<br />
<br />
R女士以这些理论问题很复杂，现在还真说不清楚予以回避。<br />
<br />
R女士看出我的倾向，于是换了一个话题，说民主要讲国情，中国国情不适合民主，只能用专制的手段来统治（看来在国家专制定性上倒是不谋而合），小的说各个单位，民主管理就乱套，有个强势领导就发展很好。<br />
<br />
我回答说，国外的企业都不是民主管理，无论微软还是通用，大型上市公司还是小型合伙企业至少没有什么职工代表大会。这是因为企业有明确的产权，产权所有者有最终索取权，自然要对企业负责。受雇人员是劳动力（包括智力）的买卖关系，用脚投票市场决定，当然不用政治民主程序。公共部门的负责人就必须是民主产生，至少是特定阶层的民主产生，因为作为公器掌管者没有如企业那样的谋私利的题中之意，必须接受公众监督和由体现公众意志的机构任免。中国的事业单位是计划经济的怪胎，应该明确地把一部分归入企业，一部分归入政府。<br />
<br />
R说：“某某问过欧盟代表，你们能用整个欧洲的全民公决来决定环保，财政开支等等公共问题吗，欧洲才7亿人口，中国人口是欧洲的两倍，怎么能通过这种方式政治运作。”<br />
<br />
我回道：“这是某某不懂现代民主运作。现代民主是代议制，诸如环保，财政开支等都是议会下的专业委员会的专家提出意见，再拿出来审议和议会表决。让一个人表决他不懂的事情不是民主。关于是否独立一般都是公决，因为这个是利益和传统考量，专业技术含量少。”<br />
<br />
R女士反问：“你觉得让中国8亿农民参加普选现不现实，中国人的素质是不是达到了这个高度？”<br />
<br />
我说：“毛主席说过，要从战争中学习战争。中国人也应从民主中学习民主。从总体上来说，不民主永远也适应不了民主。中国现在不是在搞村级民主选举吗，认为他们不会民主，是不是承认这个民主实践是假的？另外，民主也可以循序渐进地搞，比如既然党员由中国的先进分子组成，先进分子的素质应该是高的吧，可以从党内开始搞民主吧，中央委员会的素质更高，可以搞总书记差额选举，搞竞选吧？”<br />
<br />
我还说，我希望我党永远执政，希望能代表民族利益。但是我希望这种执政有法理上的国民认同，而不是自己宣传的人民支持，我希望自己的党不怕批评，不怕有反对派，这样才理直气壮是人民的选择，有监督有竞争，才能避免犯错误。<br />
<br />
酒桌上在座大多是体制内官员，有校长，还有党委书记。我原以为他们观念上会趋向保守，不会赞成我的观点。没想到的是，我说话时，能感觉到他们不时地投来的赞许目光。<br />
<br />
一直在我旁边沉默不语的意识形态主管这时说话了，他说：现在我们国家的问题，一个是政治体制改革滞后，拖了社会发展的后腿；另一个是人权没有得到很好的落实。<br />
<br />
我很惊讶，同时很感激地向这位老大哥看去。我说：“说得太好了，这才真正代表了我党的希望。”<br />
<br />
R女士笑着说：“这样的希望在党内占大多数。”<br />
<br />
我也笑着再次面向R女士，问道：“在这个问题上你的想法呢？”<br />
<br />
R女士同样笑着说：“我没有自己的想法，我的想法都是党的想法”。<br />
<br />
我不知道意识形态老大哥的想法是不是在党内占大多数，但我知道这样的希望在这个饭桌上不在少数。<br />
<br />
有意思的是：在餐桌上，意识形态主管坐在我的左边，R女士坐在我的右边。<br />
