													<h2>李大眼：让我深情地、最后地剽你们一次</h2>
						
						
																											<div id="postmessage_527342" class="t_msgfont">郑曦原编三联书店出版的《帝国的回忆――〈纽约时报〉晚清观察记》341页<br />
<img src="http://pic1984.12hp.de//up/1269263329.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
<a href="http://blog.sina.com.cn/s/blog_46e7ba410100hh7d.html" target="_blank">http://blog.sina.com.cn/s/blog_46e7ba410100hh7d.html</a><br />
<br />
<br />
在我正式关闭微博之前，决定深情地、最后地剽一次：<br />
<br />
“中国办有报纸，但遗憾的是中国的编辑们不愿将真相告诉读者，他们不像你们的报纸讲真话……由于不能诚实地说明真相，我们的报纸就失去了新闻本身的高贵价值，也就未能成为广泛传播文明的方式了。”大家知道，这是我剽黄健翔的，而黄健翔是剽张发财的，张发财是剽《帝国的回忆》的，帝国回忆是剽《纽约时报》的，《纽约时报》是剽李鸿章的。<br />
<br />
 <br />
我不仅剽了这句话，而且还剽了黄健翔关闭微博这个动作，而黄健翔又是剽了韩寒的动作，韩寒可能也受到了易中天冷处理微博的启发从而关闭……子子孙孙无穷尽，瓢瓢罐罐无止境。前两天一直在劝黄健翔不要关闭微博，20分钟前才发现也有人说我也剽过一段叫“更俗”的段子，还在包括现代快报等报纸上以律师名义说我剽了，因为别人先发博。在此，郑重地向被我剽过的先博们道个歉，我错了，剽是比嫖更严重的事情，因为嫖带不来名利，而剽是可以的。在此，不仅注明最早发的是一个叫“露透社”的朋友，而且我会为此付出稿酬（朋友你收稿费的地址可发我邮箱<a href="mailto:lichengpeng@vip.sina.com">lichengpeng@vip.sina.com</a>，不日寄出）。<br />
<br />
 <br />
关闭微博不是给黄健翔助拳，因私人关系好就去助拳不是一个好习惯，至少我们在假装独立的知识份子精神。关闭是因为纯中国式的技术原因，微博的重要功能就是分享和转发，比如手机上接到一调侃政府的段子让你心里痒痒特别想让更多的老百姓知道，比如MSN上朋友发来一句讽刺二会代表的话很有趣你想再深喉一下，但你很难找到原主，要是你把发来短信的朋友当成原创，朋友就剽了，把手机当成原创，那中国移动就剽了。虽然很长时间来我小心地在转发的段子后面注明“转发”，但有人说这也是剽，因为没有详细注明转发自哪里。这个表面看可以，其实很难，我试过转发自“校内”，后来后来有个人就指责这“校内”其实是转发自土豆的，其实这就是校内剽了，而土豆又转发自凯迪的，凯迪可能转发自天涯的，有的甚至只是某篇新闻后面留言里的，这样下去，无论怎么详细注明都是剽了的，微博客就是剽博客了，微博就不归新浪而归扫黄打非办管了。<br />
<br />
 <br />
新浪微博是源自于美国推特儿（这也是剽），推特儿的宗旨是通过转发和分享让更多的公众获取公众信息，这是民主的基础，可这种玩法在中国出了点小问题，满大街追着买盗版碟并在创作中抄来抄去的文人忽然特别在乎几十个字的也不知道究竟是谁的产权，在乎当然是对的，但在中国商标注册都需要18个月的时长，以后我们发一条完美无缺的微博，就需要18个月。这很无懈可击，但会让人泄气，最后脑力活儿就变成了体力活，微博写手也会成为专案组公安。当然也有办法――以知识产权为由大幅度减少发微博的次数，跨省寻找原创的人，最大限度禁止转发那些非自己写出来的有趣地调侃时事的贴子，干净、肃静、合法、简约，于是一个旷世奇迹就出现了：<br />
<br />
 <br />
D和政府忽然开心地发现，绿坝、过滤软件、五毛党做不到的事情，已被文人们自行做到了，因为有一个貌似知识产权的东西让那些不和谐的段子大大减少了，因为文人不仅相轻而且文人还相亲，总因为跟谁谁更亲近就迅速地拉帮结派去灭掉另一拨文人，加之文人更有文彩，更有逻辑性，更知道利用知识产权这种武器和揭发隐私这种暗器，还不用国家花钱，这比五毛强得多，这叫以夷制夷，然后D和政府就安全了，和谐社会从此站起来了。<br />
<br />
 <br />
中国文人从来喜欢争宠，表面看毛发耸立像匹狼，其实一直渴望成为宠物，只不过机遇还没到，如果机遇到了他们会撒着欢向朝庭奔去，比朝庭还看不起那些还未成为宠物的狼，发自内心觉得狼长得太难看，并呸呸着口水。现在大家都在骂五毛，其实五毛是很可怜的，现在伪装很容易被识破，花销又大，通货和政府的信心又在一起膨胀，文人就不一样了，实力高很多，长得又好看，他们不是五毛，是金毛，结论是，金毛是比五毛更有效率的一种宠物。<br />
<br />
 <br />
金毛的内心有一团饥渴的火（他们互相之间称之为创作热情），但他们不会评论欺实马，不会抗议暴力拆迁，更不敢调戏二会代表和D，这样太不安全了，也没有实际好处。那团火又必须得出，怎办，找没有组织没有背景没有黑社会支撑的人爽一回吧，比如早些年的王朔后来的郭德纲、韩寒，再后来的黄健翔李大眼之流。发现这一点金毛们高兴呵，就拼着写呵写，把这个搞成了赛诗会，内部之间也达到了空前的团结。回到李鸿章说的中国报纸不像外国的报纸，是不能成为广泛传播文明的方式的。报纸是西方人发明的传播文明的方式，但在中国成不了，推特儿是美国人发明的方式，在中国也成不了，文明的互联网这样，文明的微博这样，文明的知识份子也这样。很多传奇般的文明产品在中国都成不了，这本身就是世界文明的最大传奇。<br />
<br />
 <br />
当然参与到这次论战中的人不是金毛，也不是五毛，他们都是很有才华的人，而且我觉得黄健翔错了，不该发飙更不该向老同志陈村发飙（陈村早年的小杂文写得很好），另，既然张发财现身了他就该付稿费该注明著作权。可这事儿也不好操作，因为刚才忽然又有另一个人现身说其实张发财是剽他的，他比张发财还要早写这句话……看到这里大家就该知道这个，这个很难办了，出了张发财又来了张红中，保不齐又会来个张白板、张九筒。我的问题也是这样，我现在刚知道了那条“更俗”应该属于“露透社”朋友，希望不要再来一个“发薪社”。<br />
<br />
 <br />
可能上面的文字写得不够知识产权，再次真诚声明一下：虽然注明最原始的出处在中国变得很难，但这个要求却是有道理的，是合乎科学的发博观的，谁玩不下去，觉得不好玩了，就别玩了。微博把这篇分段贴出去后就正式关闭了，关闭，是因为想闭关，人的精力是有限的，除了找出处太难，没有一分稿费还得天天趴在电脑前，最重要的是从技术上来讲它很破坏写博客的灵感，本来一篇气息连贯的文章，把其中几十字先弄到微博上去，文章也没办法写了，从个人写作观，觉得纪录这么神奇的时代和国度，博客还是更适合我一些，更像重武器，趁手。<br />
