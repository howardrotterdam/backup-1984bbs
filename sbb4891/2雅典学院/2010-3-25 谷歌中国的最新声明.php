David Drummond, SVP, Corporate Development and Chief Legal Officer<br />
<br />
今年1月12日，我们在本博客上宣布，<strong>Google及另外二十余家美国公司受到了来自中国的、复杂的网络攻击，在对这些攻击进行深入调查的过程中，通过我们所收集到的证据表明，几十个与中国有关的人权人士的Gmail帐号定期受到第三方的侵入，而这大部分侵入是通过安装在他们电脑上的钓鱼软件或恶意软件进行的。</strong>这些攻击以及它们所暴露的网络审查问题，加上去年以来中国进一步限制网络言论自由，包括对FaceBook、Twitter、YouTube、Google Docs 和 Blogger 等网站的持续屏蔽，使我们做出结论：我们不能继续在Google.cn搜索结果上进行自我审查。<br />
<br />
从今天早上开始，我们已停止了在Google.cn搜索服务上的自我审查，包括 Google Search （网页搜索）、Google News（资讯搜索）和Google Images （图片搜索）。 访问 Google.cn 的用户从现在开始将被指向Google.com.hk，在这个域名上，我们将提供未经审查的简体中文搜索结果，这些为中国大陆用户设计的服务将通过我们在香港的服务器实现。香港地区的用户还将继续通过Google.com.hk获得跟现在一样的、未经审查的繁体中文搜索服务。在我们进行迁移的过程中，由于香港服务器负荷的增加以及这些变化的复杂程度，用户可能会发现搜索速度变慢，或发现某些产品暂时不能访问。<br />
<br />
实施我们做出的在Google.cn上停止审查搜索结果的承诺是一个十分艰难的过程。我们希望全球尽可能多的用户都能访问到我们的服务，包括在中国大陆的用户。中国政府在与我们讨论的过程中已经十分明确地表示，自我审查是一个不可谈判的法律要求。为此，我们相信，一个解决我们所面临挑战的可行方案是在Google.com.hk上提供未经审查的简体中文搜索结果――它完全符合法律要求，同时也有助于提高中国大陆用户对信息的访问。我们十分希望中国政府尊重我们的这一决定，尽管我们知道，用户对Google服务的访问有可能随时被阻止。为此，我们将密切监测网址访问问题，并制作了一个新页面，用户可以实时地了解到在中国哪些Google服务是可用的。<br />
