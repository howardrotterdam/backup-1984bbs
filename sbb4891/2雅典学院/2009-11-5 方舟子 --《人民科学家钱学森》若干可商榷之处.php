													<h2>方舟子：《人民科学家钱学森》若干可商榷之处</h2>
						
						
																											<div id="postmessage_324351" class="t_msgfont">方舟子《人民科学家钱学森》若干可商榷之处<br />
时间：编辑时间：2009/11/07 出处：生物通<br />
　　　　　　　 ・方舟子・<br />
<br />
　　手头有一本上海交通大学出版社出版的“杰出交大人丛书”《人民科学家钱学森》（2002年12月版），作者为“跟随钱老工作、生活了20年”的涂元季。据作者在前言中介绍，该传记初稿的写作得到了钱学森的同意，并要求作者写作时“要实事求是”，作者也称：“我希望在有生之年能奉献给一个广大读者一个客观的、真实的和比较全面深刻的‘钱学森’。为了完成这项艰巨的任务，我诚恳地希望读过这本小册子而又熟悉情况的读者，能对此书提出补充、修改，甚至批评、指正意见。”我就根据其希望，提几条意见。<br />
<br />
　　一、<br />
<br />
　　“与此同时（指1944年），加州理工学院提升钱学森为讲师。”(p.18)<br />
<br />
　　根据加州理工学院的资料，此时（1943－1946）钱学森的职称应为航空学助理教授（Assistant Professor of Aeronautics）。1946年钱到麻省理工学院当副教授、正教授，1949年回加州理工学院任Robert H. Goddard正教授和Guggenheim喷气推进中心主任，直到1955年离开美国。<br />
<br />
　　二、<br />
<br />
　　该书提及钱在1945年是美国空军科学咨询团团员(p.18)，却未提及钱当时被临时授予上校军衔。<br />
<br />
　　三、<br />
<br />
　　该书声称1948年“祖国的解放事业胜利在望”，钱开始准备归国，1949年钱加紧了回归祖国的准备，以便实现他多年的夙愿（pp.20-21）。却未提恰恰是在1949年，钱申请加入美国国籍。我们很难理解，一个已在美国生活了14年的人，却在中国大陆江山易主、准备回中国时突然要求成为美国公民。在钱提交美国入籍申请后，1950年6月6日，美国联邦调查局怀疑钱曾经加入美国共产党，取消了他参与机密研究的资格。此前钱的研究工作几乎都属于军事机密，取消其参与机密研究的资格相当于在很大程度上终结了他的研究工作，因此钱向加州理工学院校方宣布，由于他已成为“不受欢迎的客人”，他准备回中国。在加州理工学院校方的要求下，钱申诉要求恢复他参与机密研究的资格。此后美国移民局启动将钱驱逐出境的法律程序，钱也为了能还其清白留在美国而抗争。<br />
<br />
　　四、<br />
<br />
　　 “1950年6月，两名美国联邦调查局的人来到钱学森的办公室，指出钱学森30年代在加州理工学院时的几位朋友都是共产党员，而威因鲍姆家的聚会实际上是共产党的小组会议。在1938年小组的一份成员名单里，有一个叫约翰・德克尔（John Decker）的名字。由于查不到此人的下落，于是，他们指控钱学森化名约翰・德克尔，是共产党员，属非法入境。他们还要求钱学森提供证据，指证威因鲍姆是共产党员。”(p.21)<br />
<br />
　　根据《中国云》（The China cloud: America's tragic blunder and China's rise to nuclear power, by William L. Ryan, Published in 1968, Little, Brown (Boston)）一书的介绍，情况是这样的：钱在1938年曾多次参加美国共产党在加州理工学院的领导人威因鲍姆（Sidney Weinbaum）组织的聚会，这些聚会表面上是音乐会和读书会，但是是美国共产党拉拢同情者和发展党员的渠道。钱的几名同事也是美国共产党员。当时加州警方在美国共产党的线人抄录了党员登记卡，其中有一登记卡用的化名John M. Decker，真名则是H. S. Tsien。加州共产党也用党员登记卡来代替入党申请表。据此警方认为钱曾经化名John M. Decker申请加入美国共产党。（当时美国共产党员可以给自己取一个化名或“党名”掩盖身份）<br />
<br />
　　五、<br />
<br />
　　 “钱学森当机立断，决定马上以探亲为名回国，……海关扣压了钱学森的所有行李，污蔑他企图携带‘机密资料’出境，触犯了‘出口控制法’，勒令他‘不准离境 ’。尽管钱学森生命，所有带机密性质的东西都锁在办公室的保险柜里，钥匙已交给克拉克・米里肯（Clark Millikan）。他带走的都是个人物品，他的笔记本、讲义手稿、公开资料等。所谓机密性质的蓝图和密码本，只不过是他手稿中的草图和对数表。但也无济于事，司法部还是签署了逮捕钱学森的命令。”(p.22)<br />
<br />
　　钱在告诉加州理工学院要回中国后，以探亲的名义订了8月份回上海的机票。但是他又在8月份去华盛顿找他的朋友、海军部副部长金波尔（Dan Kimball，该书误为金贝尔Kimbeel），告诉他由于被指控是共产党员，准备回国。金波尔告诉钱不相信他是共产党员，但是也不允许他回中国，同时答应帮他恢复机密研究资格。在钱离开后，金波尔立即通知国务院，不能让钱离开美国。钱从华盛顿回到洛杉矶时，移民局就向他出示了禁止离境的命令。钱取消了回国机票，但是他的行李已经装箱运到港口。法官下令没收、检查这8箱行李。发现里面有的文件还盖着“机密”、“保密”的章（钱的说法是这些文件其实已过了保密期），并一度把对数表误当成“密码本”。不过，移民局从行李中找到了他们能指控钱的证据：一张钱化名约翰・德克尔的美国共产党党员登记卡，和警方线人抄录的一致。9月6日，移民局逮捕钱，罪名是钱是加入“建议、鼓吹或教导用暴力推翻美国政府”的组织的外国人，应该被驱逐出境（美国移民法禁止这样的外国人入境。钱在1947年回国探亲、结婚回美，在檀香山入关填表时，对是否是这种组织的成员，写了“No”）。钱在被囚禁2周之后，交保出狱候审。<br />
<br />
　　六、<br />
<br />
　　“期间，联邦调查局和移民局为查清钱学森是否共产党员，还多次举行所谓的‘听证会’，对钱学森进行审讯。”(p.24)<br />
<br />
　　美国官方举行听证会的目的，是为了证明钱学森应该被驱逐出境，为此要查清钱是否共产党员。虽然美国官方从钱的行李中查到了做为入党申请表使用的党员登记卡，但是他们并不能证明钱提交了这份申请表，表上也无钱的签名。<br />
<br />
　　“检察官是一个极端刁钻的家伙，他在一连串例行提问以后，突然问钱学森忠于什么国家的政府。”(p.24)<br />
<br />
　　然后该书用了近一页的篇幅栩栩如生地叙述钱学森与检察官的唇枪舌战，钱是如何义正词严地驳斥检察官，检察官又是如何地狼狈不堪。“第二天洛杉矶报纸上的大字标题是：‘被审讯的不是钱学森，而是检察官’！”(pp.24-25)<br />
<br />
　　对钱的听证是在一间十几平方的密闭小房间进行的，钱由其律师陪同，很可能没有记者在场，所以我怀疑当时的报纸是否报道了听证会上的问答。该书对听证问答的描述，要么出自钱本人后来的回忆，要么是作者自己的想像，与事实出入很大。根据听证记录，我把实际的问答翻译如下：<br />
<br />
　　问：你效忠共产中国吗？<br />
　　答：不。<br />
　　问：你效忠谁？<br />
　　答：我效忠中国人民。<br />
　　问：在本国和红色中国发生冲突时，你是否会为了美国而与红色中国作战吗？<br />
　　答：我现在不能回答这个问题。<br />
　　问：你现在不能回答还是不愿回答这个问题？<br />
　　答：我现在能回答这个问题，我的回答如下：我已说过我必然效忠中国人民，而如果在美国和共产中国之间的战争是为了中国人民的利益，我认为这是非常可能的，那么我将站在美国这边作战。对此是毫无疑问的。<br />
　　问：但是你将先做决定？你将决定它是否是为了中国人民的利益？<br />
　　答：是的，我将做这样的决定。<br />
　　问：你不会允许美国政府为你做决定？<br />
　　答：不，绝不会。<br />
<br />
　　1951年4月26日，听证官宣布其决定：钱是一名外国人，由于发现他在入境前（指1947年重新入境）已是美国共产党的成员，因此应该被驱逐出境。<br />
<br />
　　但是由于钱知道太多的军事机密，美国司法部却不许钱立即离境。钱继续在加州理工学院任教，但是不能擅自离开洛杉矶，必须每月向移民局报告，当然也不能参与机密研究了。又过了4年，1955年8月中美为朝鲜战争的战俘问题在日内瓦举行谈判，允许钱离境被做为中方的条件之一提出来（该书的说法是钱在1955年 6月写信请求中国政府营救他回国），绝不允许钱离境的金波儿已下台，美国司法部显然认为时隔5年已足以让钱掌握的军事机密过时，在1955年9月17日将钱驱逐出境。传说中方用了11名被俘美军飞行员做为交换，美国政府则否认这种说法。<br />
<br />
　　七、<br />
<br />
　　钱回国后，除了领导导弹和火箭项目，很著名的一件事是在大跃进期间创建“农业力学”，在1958～1959年间先后在《人民日报》、《中国青年报》、《知识就是力量》发表三篇文章，为粮食亩产万斤提供“科学”证明。该书对这一震惊世界的著名事件提都不提，也无如何解释。<br />
<br />
　　八、<br />
<br />
　　对钱学森在文革中的表现，该书提到一件事：1970年7月钱调到国防科委担任副主任，林彪办公室要他们编一本关于科学技术发展的小册子。编好后林办很满意，送给钱一筐芒果致谢。钱以领导的心意领了，自己不接受任何人的礼品为由，把芒果退了回去。“敢于谢绝‘林副统帅’的‘好意’，那可是要冒很大风险的。 ”“这，就是钱学森的政治品质和政治智慧！”(pp.76-77)<br />
<br />
　　不过该书却没有提及更能显示钱在当时的“政治品质和政治智慧”的另一件更重大的事件。据1970年8月24日中共九届二中全会笫六号简报（华北组第二号简报）：“为了反击这种反革命阴谋活动，钱学森同志首先建议在宪法上，第二条中增加毛主席是国家主席，林副主席是国家副主席，接着汪东兴同志进一步建议宪法要恢复国家主席一章，大家热烈鼓掌，衷心赞成这个建议。”这份简报后来被毛泽东认定为是“反革命简报”，第二天下午就被毛泽东下令收回，全会也因此立即休会。此事成为毛泽东与林彪摊牌的导火线。<br />
<br />
　　九、<br />
<br />
　　“人体特异功能研究”是“钱学森晚年倾注大量心血的一个领域”（司马南语），但该书仅以不到一页的篇幅概述钱学森对“中国人体科学”的一些比较低调的看法 (pp.133-134)，对钱学森在这场“中国人体科学”运动中发挥的领导和指导作用，为“人体科学”唱的种种高调，都忽略不提，未免太对不起钱学森倾注的大量心血。<br />
<br />
　　十、<br />
<br />
　　“在他1955年回国前夕，他的老师、世界著名力学大师冯・卡门对他的评价是：‘你现在学术上已超过我了’。为此，他激动得彻夜难眠。”(p.146)<br />
<br />
　　冯・卡门对钱学森无疑是非常欣赏的，钱当时被认为是其学术继承人。但是这个评价不见于冯・卡门的回忆录或文字材料，从上下文看大概是钱本人转述的。对这种空口无凭的私下交谈，当事人激动可以，别人最好还是不要太当回事，毕竟，在国际上，没有人会认为钱的学术成就超过了冯・卡门。如果钱学森像国内某位化学院士评价的那样是全世界每十年才出一个的科学家，那么冯・卡门这样的科学家岂不是百年才出一个？其他领域像冯・卡门这种级别的科学家还有的是，这一百年来恐怕就出了上百个百年一遇，上千个十年一遇的科学家了。<br />
