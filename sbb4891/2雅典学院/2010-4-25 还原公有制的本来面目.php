																											<div id="postmessage_584166" class="t_msgfont">中共几十年来的宣传，给大家几个错觉<br />
<br />
1. 只有社会生产力高度发达，物质极度丰富，才能建好社会主义，才能走向共产主义<br />
2. 号召大家为共产主义而奋斗终生，好象共产主义就是明天或几代人的事情<br />
3. 公有制就是国家所有，集体所有，只要是国家的了，是集体的了，就是公有制或国有制<br />
<br />
以上三个问题都是伪命题，都是谬误的<br />
<br />
1. 实现社会的公平正义，建设夜不闭户的大同社会，与生产力的高底，社会的物质丰富并无关系，我们可以很容易的看到，在一些落后的山区人民，人民的幸福指数并不一定比发达的大城市底<br />
