													<h2>还原公有制的本来面目</h2>
						
						
																											<div id="postmessage_582001" class="t_msgfont">还原公有制的本来面目，打破中共的谬误理论垄断，才是促进社会变革的王道<br />
<br />
今日首次注册1984，翻出一篇旧文同大家共同探讨，此文所涉及的话题决非三言两语可以说的明白，但是一个引子，希望对此感兴趣的朋友可以参与讨论.<br />
<br />
公有制并不是社会主义专有的属性，事实上公有制是伴随着社会的产生而产生的，在任何社会性动物群体中，公有制都是存在的，没有公有制就没有社会性。社会性是指动物群体之间通过共享某种资源而具体有的属性，这种资源包括食物，居所，道路，水源，生活活动场所，集体防御力量等等。<br />
<br />
通过对最原始的公有制分析，我们可以了解到公有制的两个属性，一个是这些是资源是公有的，另一个是全体社会成员共享由这些资源所产生的效益，或称社会效益。另外我们可以看到这些资源是不会增加的，随着社会效益的输出，公有资源会变的越来越少。但在社会性动物群体中，各个个体成员都会不断地进行劳动，通过这种无偿的劳动，使得公共资源得以维持并适度增加，以满足群体发展的需要。这种无偿的劳动，我们可以称之为服务(贡献)。这也许就是中国共产党理论中为人民服务的服务一词的来源了。所有的服务都是无私的，或称之为不会产生利润的。<br />
<br />
通过以上分析，我们可以得出结论，公有制有三个基本属性，即公共，共享，相互服务(贡献)。<br />
<br />
现在我们再来分析马克思理论中私有制和公有制之间的差异之处。马克思对私有制的两个属性资本和剩余价值(即利润)，进行了详细的分析和解释。但是由于马克思所处的时代局限性，对公有制却言之不详。这就导致了前苏联和中共把社会主义公有制理解为全民所有制下的(国有)大生产。在中共现有的理论中，公有制除了资本归国家占有外，和私有制并无区别。即国有资本仍具有私有制的一切属性，也就是说国有资本具有对剩余价值(利润)贪婪和追求的属性。我们也可以说国有制是由国家这个最大的资本家占有生产资料的私有制的一种表现形式。要想国有制等于公有制，必须使得国有资本不以产生经济效益(利润)为目的，真正的公有制是只产出社会效益，而不产生经济效益的。<br />
<br />
我们再看一下当今发达资本主义国家的公有制。公有的学校，医院，交通，国防，及各种福利机构等等，都具备公有制的特征，即公共拥有，共同均等享有，且共同对这种公有制进行贡献(交税)。同时在当今发达的资本主义国家，第三产业即服务业非常发达。服务业本身即具有公有制的属性。因为服务业生产的商品均用于消费，或是一次性消费的，不具有再生的可能。<br />
<br />
从一定意义上说，当今发达资本主义国家已进入了资本社会主义国家，全体资本平等享有社会的公共资源，全体个体也平等享有社会的公共资源。当公有制的资产，包括第三产业在社会中所占的比例越来越大时，这个国家也就进入了社会主义，当私有制资产在社会中所占的比例越来越小时，直到消失，人类社会也就进入了共产主义，当然这只是一个极限状态。<br />
<br />
<br />
我们再来看一看我们所谓的社会主义国家实际上只是国家私有资本主义，或称之为官僚买办资本主义。在所谓的国有企业中，其资本为政府占有，资本的存在就是为了产生剩余价值，这一点同私有资本并无不同之处。不同之处在于，在资本主义社会，资本之间是存在竞争的，且是在同一游戏规则下进行的。但在官僚买办资本主义条件下，国有资本及私有资本会同政府联合起来会对人民进行更为严重的剥削。<br />
<br />
但在我们的国有企业中，因为多是垄断行业，是不存在竞争的问题，同时政府不是做为游戏规则的制定者，而是直接参与者，也就是国有企业和政府均是为了获取更多的剩余价值。这就导致国有企业对人民存在更严重的剥削相象。当然这种剥削不是直接的，而间接的。为什么呢？因为国有企业的管理者同企业的经济利益没有直接的关系，国有企业的管理者的身份首先是政府官员，其次才是企业管理者，同时由于政府官僚之间关系的复杂性，使得国有企业的效率低下。国家为了维持垄断企来的正常运转，势必对其投入大量的资金，而这些资金，是全体人民贡献税金，理应用于公共事件，使全体社会成员共同享有的财富却被低效率的国有企业给消耗掉了。</div>

							
							
							
							
