　　普京之所以受俄罗斯老百姓的欢迎，不全是因为石油涨价他有了讨好老百姓的资本，而是他迎合了老百姓的一种心理需求，他要求教科书修改对前苏联的评价，修改对斯大林的评价，不是不知道斯大林的罪恶，不是内心里真的肯定斯大林，也不是为了把自己往里面套，而是他知道，尽管学校里的教授老师、报社里的编辑记者一致疯狂“诋毁”前苏联的一切，但老百姓依然希望，自己民族是拥有值得骄傲历史的民族，虽然全体俄国人都知道，从腐朽的罗曼诺夫王朝到残暴黑暗的苏联，从来就没有碰到过像样的政府和领导人，但如果一个自诩为伟大的民族，近现代以来的历史被描述为从头到尾全部是在黑暗政治之中的历史，这样民族颜面何存？<br />
