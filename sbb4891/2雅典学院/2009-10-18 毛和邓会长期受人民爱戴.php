																											<div id="postmessage_289090" class="t_msgfont">很凑巧，今天我也在思考一个类似的问题：关于对“中国人”的称谓。<br />
<br />
这个国家的主体，或者说承载了所谓中华文化的主要族裔，自称为汉族，因为汉朝曾经在东亚大陆搞过大一统，也曾经疆土辽阔。<br />
<br />
在海外的中国人，搞的定居点叫唐人街。先不讨论李氏家族是否真的有胡人背景，因为哪怕他们真的是某种胡人（比如鲜卑），他们早就选择了自我的文化灭绝，连姓氏都改成汉人的姓氏，所以也只能认为他们已经成为了汉人。<br />
<br />
至于现在嘛，贵国从KMT的南中华朝（定都南京），变成了北中华朝（定都北京），虽然一个皇帝复姓国民，一个复姓共产，但其实爸爸同意姓俄，这和当年东西周东西汉南北宋的更迭是一样的。国号都叫中华，一个“大一统”的中华。<br />
<br />
我们可以发现这三个朝代很明显的特征是1、没有很明显地被外来民族长期打击（中华朝有之前的20多年“而已”）并且在疆域上多吃多占；2、最后不是被外来民族搞死的，是“自己人”搞死的（所以就算假定明朝在历史上曾经很牛逼，能够派出几百个太监坐太空船探人马座阿尔法星，只要他是被女真人搞掉的，都无法荣升此列；本朝暂时还不知结果，不过根据世界大势，以后估计很难再打这种一个民族要直接奴役另外一个民族的仗）。<br />
<br />
与之相比，那些短命的朝代，各地割据的时期，以及被外来民族日得上吐下泻的朝代（宋、明）就不会被汉族、唐人，或者说华人（近现代发明来用作大一统的概念）用来当作自己的称谓。至于“外族”统治时期（元、清）就算再大一统到欧洲去，也不会当作是自己的名字。<br />
<br />
这就是这个“民族”的叙事方式。但其实中国人和外国人没有什么不同，所以我觉得这也是“民族”这种东西的叙事方式。<br />
