													<h2>再论拒绝五毛言论并不违背言论自由原则</h2>
						
						
																											<div id="postmessage_673395" class="t_msgfont">据说，法国启蒙r代思想家、哲学家、文学家伏尔泰有这样一句话：“我并不同意你的观点，但是我誓死捍卫你说话的权利”（I disapprove of what you say, but I will defend to the death your right to say it.）。也有人认为伏尔泰并没有说这句话，是另外一个人（Evelyn Beatrice Hall）在出版传记《伏尔泰的朋友们》一书中，记叙伏尔泰的主张时所表述的。<br />
也许，这句话在那个政治制度、人文环境下，有它特有的意义与分量。但是这句话在当下中国，在近世纪最黑暗的政治环境下，不仅是一个诗歌童谣式的梦话，甚至，是一句毫无实际意义的牛皮大话、空话。<br />
实现言论自由，首要条件就是要有可供自主选择的载体，比如出版、集会、公共论坛等。现实情况是，我们每一个政治异议者连自己的言论权利都捍卫不了（甚至人身安全都成问题，例如刘晓波），韩寒的博客文章经常遭遇刀斧手，所办杂志难产至今，章诒和的记叙是大陆禁书等等等等，我就搞不明白的是，怎么总有人觉得自己很有料，时不时的就要捍卫共产党豢养的多如蝗虫的网络评论员的“言论”权利，他们的权利是你能捍卫得了的么？这种所谓的“权利”（我看其实是他妈的权力）是你捍卫不捍卫就能改变的么？这些人要表达的政治意见以及价值观，充斥了所有可以想象得到的媒体，充斥了所有可以碰得到的角落。有的人实在是想出名搏位的话，还可以用自己的钱为党为国为赤色政权写书撰文评功摆好，完全不受舆论控制的刀闸限制。那些背诵着伏尔泰名言的冬烘们，除了不同意人家的意见以外，只有接受权力暴力安排下的现实的唯一选择，说什么“誓死捍卫”的话，不是自作多情么？<br />
试想，万一哪一天你有机会出书、办杂志、办栏目，你是按照你自己的思想表达自己的见解与主张呢，还是任意让人左右，塞进一些乱七八糟，不可预见，不可控制的争吵垃圾呢？如果社会提供的机会是公平的，我认为，正确的做法是，你我如果意见相左，又不适合塞进一本书刊、栏目的的话，我们还是各写各的，各办各的吧！当然，如果你存心想让你的政治对手出丑或扬名，或是非要为党国舆论特务出书刊、办栏目出钱出力的话，我只能说“你有病，我服了你”了！<br />
网络论坛其实与此同理，很多人包括我从未主张过要封掉《乌有之乡》《铁血论坛》这样的五毛、毛左、愤青基地，这应该算是信守自己的言论自由原则了吧！为什么某些人却偏偏期望《雅典学院》就非要装他妈逼大瓣蒜，搞成黄泥与稀屎共处，林冲与高俅同桌的打斗场和杂货铺呢？高俅难道没钱自己搞一桌么？非也。<br />
话说到根上，才能滋润意思的枝叶。言论自由说到底，是需要宪法、法律和社会政治规则来保障的，不是靠道德自律、自作多情式的“誓死捍卫”来保障的，维护一整套完整的法律机制来保障公民人权，比背诵美妙空洞的伏尔泰名言，要可靠一万倍。</div>

							
							
							
							
