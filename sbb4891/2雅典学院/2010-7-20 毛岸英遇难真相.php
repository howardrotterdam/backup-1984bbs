毛岸英遇难真相<br />
<br />
<br />
　　毛岸英於一九五○年十一月二十五日在朝鲜前线被联合国军美军飞机炸死。笔者曾经访问志愿军总部多位工作人员，当年知情者陆续披露毛岸英遇难的一些重要细节。<br />
<br />
　　在他逝去前半年，就是一九五○年四月，毛泽东在审定五一国际劳动节宣传口号时，在「中华人民共和国万岁」、「中国共产党万岁」后亲笔加上「毛主席万岁」一条。一九四九年中国人民政治协商会议制订的《共同纲领》确定中国的国体是人民共和国，何来其领导人「万岁」，显然毛泽东已经钦定自己是「万岁爷」了！<br />
<br />
<br />
　　朝鲜前线「镀金」，张狂极致<br />
<br />
　　毛岸英是毛泽东的长子。按照中国帝制传统，长子是法定的第一承继人，即皇太子。毛岸英先在苏联留学，回国后在农村和工厂体验过，前往朝鲜前线镀金期间，把皇太子的张狂发挥到极致。<br />
<br />
　　一九五○年十月，毛岸英作为中国志愿军总部的俄文翻译，随同彭德怀司令员出国。当年彭德怀军事秘书杨凤安说：「办公室的成员对他（指毛岸英）也很尊重，除俄语翻译外，办公室未分配他作战值班任务。」（《时代潮》二○○四年第十九期）<br />
<br />
　　原志愿军总部作战处副处长、后潘阳军区参谋长杨迪将军，在其所著《在志愿军司令部的岁月里》一书中，曾经说到毛岸英：会议中也发生了我想不到、也不可能想到的奇异插曲（可能也出乎参加会议的领导同志意料之外），就是正当彭总向（第三十八军）梁兴初军长生气、批评梁后，与会领导同志都处在沉静严肃的气氛中时，随彭总来的那位年轻俄文翻译（我看他和我的年龄差不多，二十七八岁）却毫不胆怯地站起来，指著煸谇缴系牡赝妓灯鹄戳恕Ｅ碜茏著一句话也不说，既不制止他讲话，也不批评他，志司几位副司令也不制止他，各军军长低著头也不吭声。那位年轻的翻译，并不懂军事，我没有听明白他在讲什么，他说了一、二分后，看没有人理会他，也就不说了。当时我觉得很奇怪，怎么一个年轻翻译会在志司党委召开的作战会议上，而且是在彭总生气的严肃气氛中，敢於随便说话呢？还没有人制止他、批评他？真怪！<br />
<br />
　　会议开完后，我对（作战处）丁甘如处长说：「这个小翻译胆子真大，敢在彭总生气时，还在那儿说三道四。看来他还不懂党内和军内的规矩，这样重要的高级会议，哪有他讲话、发言的资格。他是谁？他是什么人？」<br />
<br />
　　丁甘如同志说：「老杨，你就不要问，也不要打听了，我不会告诉你，其他的同志也不会告诉你的。」（该书第58─59页）<br />
<br />
　　「彭总坐著一句话也不说，既不制止他讲话，也不批评他，志愿军几位副司令也不制止他」──这话的潜在意思是，彭总和几位副司令可以而且应该制止他讲话，可以而且应该批评他，但是都没有！按照中国共产党的政治体制和军事体制，党内军内等级极为森严，就像上引杨迪将军文中说到的，「这样重要的高级会议，哪有他讲话、发言的资格。」<br />
<br />
　　而且，这样一个纯粹是志愿军党委召开的高级军事作战会议，当然没有苏方人员参加，也就没有翻译任务，这就是说，他不仅没有发言的资格，连参加这样的会议的资格也是没有的。<br />
<br />
<br />
　　违纪炒蛋炒饭：送命<br />
<br />
　　一九八五年五月六日，时任成都军区参谋长的丁甘如将军，在军区将军寓所向笔者介绍了志愿军总部被炸前相关情况和毛岸英殉难情况。<br />
<br />
　　他说，毛岸英遇难前一天（十一月二十四日）傍晚，彭总、邓华、洪学智等总部领导人在驻地附近散步，他们边走边聊，走著走著，邓华副司令员说：这架敌机怎么还在这儿转呢？洪学智副司令员说：转了个把小时了。几位领导人几乎同时说：看样子，我们总部的目标暴露了。<br />
<br />
　　散步归来，当晚九时，志愿军党委在彭总主持下开会，紧急部署防空措施，即：布置工兵增挖猫耳洞（防空洞），并加大加固已有猫耳洞；总部所有工作人员凌晨三点起床，吃完早饭后，除必要值班人员外，四点前全部进入指定的防空地点；同时严格规定，四时后所有住处不许冒烟。<br />
<br />
　　这位当年志愿军总部作战处长说：「毛岸英三点起床了，他舀了一漱口杯子饭后，便伏在办公室桌子上睡觉了。从四点到九点多，睡了五个多小时吧。毛岸英醒来后，便就著火炉炒饭吃。」<br />
<br />
　　丁甘如接著说：「十时许，美军飞机临空，没有绕圈子，一来就投弹，而且是凝固汽油弹，直接命中彭总办公室，瞬间烈焰冲天，正在炒饭吃的毛岸英和值班参谋高瑞欣，没有来得及跑出来，不幸牺牲。」<br />
<br />
　　这里记述的过程，确切地说明，没有值班任务的毛岸英，没有遵守总部规定的三点起床后吃饭，四点前进入指定的防空地点，而是伏在办公桌上睡觉，也没有遵守不得冒烟的严格规定。<br />
<br />
<br />
　　毛岸英炒饭时的细节<br />
<br />
　　杨迪将军於二○○四年三月出版的《险难中的共和国领袖与将帅》中再次著文，进一步反映毛岸英炒饭时细节：<br />
<br />
　　在我跑过彭总办公室时，看到烟筒冒烟，立即跑进里面去看看，房里还有三个人正在用鸡蛋炒米饭吃。这些鸡蛋是前一天黄昏，我看到朝鲜人民军最高司令部派到志愿军任副政治委员的朴一禹次帅（朝鲜金日成是元帅，下有三位次帅）给彭总送来一小筐鸡蛋（约十多个）。这在当时的朝鲜是极难得的，当时彭总已吃过晚饭，还没来得及吃。三人中我只认识成普同志，那两位同志只知道一位是彭总的俄文翻译，一位是才从西北调来的参谋，他们的姓名我不知道。<br />
<br />
　　我问成普：「老成，你们怎么敢用送给彭总的鸡蛋炒饭吃呢？赶快把火弄灭。」<br />
<br />
　　成普说：「我怎么敢呀，是那位翻译同志在炒饭。」……<br />
<br />
