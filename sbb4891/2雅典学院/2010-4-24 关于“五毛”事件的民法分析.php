个人认为，大大的愤慨情绪多于理性思考。诚然，现实的残酷迫使每个有责任感的公民如大大您都会义愤填膺，但愤怒的情绪决不能替代理性的分析。不能因为统治者不重视人权，所以我们也不重视人权。以杨佳和唐福珍等人为代表的民众的人权没有得到尊重，恰恰就是我们提起异议的起点。在您看来，应该怎么处置伍皓等官员呢？砍头、凌迟、拖出去喂狗？还是继续区分阶级敌人，继续曾经的政治种姓制度？不知道您还记不记得那句话：当一个国家主席的权利得不到尊重的时候，每一个普通公民的权利也不会得到尊重。反之亦然。</div>

							
							
							
							
