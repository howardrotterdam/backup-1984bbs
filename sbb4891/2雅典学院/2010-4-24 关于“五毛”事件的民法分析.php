													<h2>关于“五毛”事件的民法分析</h2>
						
						
																											<div id="postmessage_581471" class="t_msgfont">故事内容大家基本都已经清楚，不赘述。直接分析如下：<br />
<br />
一、伍皓是否享有人格权<br />
<br />
《民法通则》第9条规定，公民（自然人）从出生时起到死亡时止，具有民事权利能力，依法享有民事权利，承担民事义务。101条规定：公民、法人享有名誉权，公民的人格尊严受法律保护，禁止用侮辱、诽谤等方式损害公民、法人的名誉。<br />
<br />
伍皓作为自然人和中国公民，具有民事权利能力，享有名誉权，不因其出身、信仰、职务而受影响。因此，认为伍皓不享有人格权的观点是错误的。<br />
<br />
二、伍皓的名誉是否受到“五毛”事件的损害<br />
<br />
“五毛”一词，除用于对一定面值的人民币称呼外，特指网络评论员，即受中国当局雇佣或指导，以网络发表评论为全职或兼职的人员。通常他们以普通网人的身份，发表尽可能对官方有利的评论，来试图达到影响网络舆论之目的。“五毛”一词通常是带有明显的负面色彩和讽刺意味，一般是为了象征性地讽刺网络评论员每发一贴能挣五毛钱。 <br />
<br />
因此，在当时的环境下，以“五毛”一词指称伍皓并抛洒五毛钱的行为，我个人认为足以使伍皓的公众评价降低，名誉受损。不论认为伍皓是“体制符号”、“奴隶主”或者是“乡愿”，这些道德、政治评价都不能证明伍皓的权利没有受侵害。<br />
<br />
三、伍皓能否请求侵权人承担侵权责任<br />
<br />
《民法通则》120条规定，公民的姓名权、 肖像权、 名誉权、荣誉权受到侵害的，有权要求停止侵害，恢复名誉，消除影响，赔礼道歉，并可以要求赔偿损失。《侵权责任法》也有类似规定，伍皓能否据此要求侵权人承担赔偿损失、赔礼道歉等侵权责任？<br />
<br />
我认为不能。原因在于作为政治人物、共产党干部，其对于名誉权的主张须受到公民言论表达自由的限制。<br />
<br />
正如许多朋友所说，伍皓当时的角色不是代表他自己――公民伍皓，而是代表了他背后的体制，他不是以自己的身份发言。因此，抗议者针对的也不是公民伍皓，而是政治人物、宣传部副部长伍皓。由于宣传部不是国家机关，所以无法适用《宪法》中公民对国家机关工作人员的批评建议权条款。但《中国共产党章程》第2条规定，中国共产党党员必须全心全意为人民服务，不惜牺牲个人的一切。第3条规定，切实开展批评和自我批评，用于揭露和纠正工作中的缺点、错误。因此，抗议者的抗议行为属于公民对政治人物、共产党员的的批评，其行为不具有违法性，伍皓的侵权请求权在此受到限制，不得要求抗议者承担侵权责任。<br />
<br />
有的朋友认为此次抗议行为侵犯了伍皓的人权。我个人认为，人权是一个集合名词，在具体事项中必须落实到具体或者比较具体的权利。在本案中，伍皓被侵犯的就是名誉权。但法律评价中不认为抗议者的行为具有违法性，因此伍皓不得要求抗议者承担侵权责任。</div>

							
							
							
							
