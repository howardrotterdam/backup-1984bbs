																											<div id="postmessage_582957" class="t_msgfont">一、对stein大大的回复<br />
<br />
1、五毛事件发生后，推上有些朋友说，伍皓作为官员不享有人格权，也有人说伍皓作为统治者没有人格。此类道德/政治评价，从法律上来说是站不住脚的。无论是谁，无论是罪犯、反革命、走资派，还是警察、革命人士，都有民法上的人格权。政治权利可以别剥夺，道德评价可以很低，但民法上的人格权是不可能丧失的。基于此，我说认为伍皓不享有人格权的观点是错误的。<br />
<br />
2、大大对于宣传部性质及法人等是否享有人格权的看法极有见地，这也是本文忽略之处。由于论述这些问题要费一些篇幅，而本文的重心并不在此，所以文中就无此内容，但大大所言仍是对我之警醒。<br />
<br />
3、我在文中有说明，伍皓是以宣传部副部长而非个人身份参会，因此其作为公众人物、政治人物应当接受公众批评。侵权责任的构成有若干要件，违法性是要件之一。而抗议者的行为尽管符合侵权责任其他要件，但由于不具有违法性，所以不构成侵权责任。文章前后是针对不同问题做的说明，应当没有违反逻辑之处。<br />
<br />
4、关于宪法和民法的关系问题，论者各有其语。据我所知，至少在美国、德国和台湾，都可以宪法作为民法审判的依据（可以参看德国梅迪库斯教授和拉伦茨教授著述，台湾请参看王泽鉴教授著述。美国更不必说）。大陆前些年曾有一例类似案例（山东齐玉苓案），可惜被最高院叫停。本文乃一时兴致之产物，考虑不周之处所在多有，还请大大拍砖指正。<br />
