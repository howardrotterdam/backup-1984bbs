													<h2>上海乐团指挥余隆是毛泽东的私生子？</h2>
						
						
																											<div id="postmessage_555030" class="t_msgfont"><img src="http://pic1984.12hp.de//up/1270734393.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;余隆<br />
<br />
from香港《文艺春秋》256期 　 作者：林涛&nbsp; &nbsp;<br />
 　 <br />
上海交响乐团下周即将到台湾演出，这是两岸文化界的一大盛事。上交近年改制，聘任“中国爱乐”余隆先生出任总监。余隆大权在握，开除全部团员，重新招考。黄钟毁弃，瓦釜雷鸣，四合院大拆迁祸延音乐界，上交百年传统毁于一旦。<br />
<br />
很多年前，我在俄罗斯新西伯利亚交响乐团客席演出，乐团经理是克格勃（KGB）转业。他见我是中国人，观众又很喜欢我，音乐会后带我游览吃饭，告诉我一则令人不敢相信的秘闻，上海乐团的指挥余隆，居然是中共中央主席毛泽东的私生子！消息来源是周恩来，原来他从黄埔军校直到死前，都是苏联特务。<br />
<br />
左王毛泽东对上海的资产阶级和知识份子，有着变态的疯魔仇恨。在“好学生”柯庆施的配合之下，全亚洲最繁华的上海，沦为一座死城。改革开放伊始，几十年没有进步的大城市，沦为一座活化石，让Issac Stern啧啧称奇。不但是具体的市政建设，共产党对人心的荼毒，触及灵魂的政治运动，扼杀了一代知识文化精英的声音。<br />
<br />
最著名的例子，上交黄贻钧指挥，当年因为太过害怕，一时紧张，喊错了口号，被共党逮捕枪毙！枪杀交响乐团的指挥，这是连希特勒纳粹党都做不出来的事。二战末期，纳粹在捷克Teresienstadt建立了一间“样板”集中营，招待红十字会观察团，犹太音乐家组织乐团，演奏贝多芬“英雄交响曲”。由此可见，中共的酷虐程度，远在纳粹之上。<br />
<br />
上海音乐学院斗翻了天，几大知名教授无一幸免。人都是有求生本能的，兔死狐悲，知名作曲家丁善德在扑天盖地的压力之下，作了两件事。一是创作了“长征交响曲”，一是献上了自己的年轻漂亮的大女儿，供毛泽东泄欲。<br />
<br />
1962年“长征交响曲”在音乐上来说，的确是一部旷世杰作。这首交响区有五个乐章，分别是“踏上征途”“红军是各族人民的亲人”“飞夺泸定桥”“翻雪山、过草地”“胜利会师”。艺术讲求“真、善、美”，很不幸的，第三乐章“飞夺泸定桥”就是一篇彻头彻尾的共产谎言。<br />
<br />
张戎大作《毛，不为人知的故事》提到，长征路上过泸定桥一事，纯属虚构。这段史实，有邓小平1982年接见美国总统卡特的国家安全顾问布列津斯基的话为证：“这只是为了宣传，我们需要表现我们军队的战斗精神。其实没有打什么仗。”<br />
<br />
丁善德有两个女儿，姐姐丁柬诺（钢琴家）和妹妹丁芷诺（小提琴家）。丁芷诺是知名的小提琴教育家，当时年纪还小，进贡毛泽东的是，正值青春年华的钢琴家姐姐。<br />
<br />
余隆生于1964年，丁大姐生了龙种，自然成为“文革”时代，一片红色海洋的弄潮女。有了这一层关系，不用说，丁善德在极左当道的年代幸免于难，后半生得意顺遂，死后备极哀荣。奇怪了，余隆从来没有交待过自己父亲的来历，父亲姓余名啥？丁善德是他的外祖父，母亲的事也不公开提，他难道是石头里蹦出来的吗？原来，这些都是说不出口的事，只有丁善德拿得出手。不知道他在2005年，丁善德去世十周年的纪念会上，低头默哀，好不伤感，心中怀念的，是不是伟大领袖毛主席？<br />
<br />
原来“余隆”者，“余下的龙种”是也。曾庆红的孙女在２００８年奥运会开幕式，和郎朗合奏钢琴一事，成为了全世界的笑谈。这位小女孩用“李木子”花名登台，共党权贵“既作婊子，又立牌坊”，子孙后代锦衣夜行，辱没家声，真是何苦呢？<br />
<br />
毛泽东的私人医生李志绥，在他的回忆录中描述，毛泽东从不洗澡，劝他就说“在女人身上洗洗就好”。彭德怀在天安城楼上，发现毛泽东的牙齿是绿色的，要求保健医生做好工作。李志绥还提到，卫士长李银桥说“主席真不是凡人，连“那种事”也不一般。”<br />
<br />
毛新宇根本就是毛泽东的亲生儿子，不是什么孙子，这在境外是人尽皆知的丑闻。毛新宇说过，小时候不能进中南海，因为江青想要杀他。江青是“四人帮”，是反派人物，所以大家不多想，总以为江青专横霸道。其实，这不过是正牌夫人见到“野种”的自然反应。那位去世不久的共和国女将军媳妇，是不是毛泽东的入幕之宾，否则为什么会捡起毛泽东吐在桌上的饭粒，往嘴里放呢？<br />
<br />
余隆是怎么发迹的呢？他是“五七音训班”出身的（表演样板戏接班人乐队），靠特权混进了上海音乐学院的大门，成为艺术院校招生的第一批工农兵学员。毛泽东的私生子余隆，和曾庆红的儿子曾伟，从小就是哥们。曾庆红的父亲是原中共“内务部长” 曾山，自然与闻毛泽东的宫帏秘事。江泽民上台之后，曾庆红进京，皇城内外自然成了他们的天下。余隆要搞“北京国际音乐节”，曾伟将自己住的房子腾出来，余隆从自己家里拿一笔钱做启动资金，又找母亲借了一辆车。曾伟对余隆说：“如果真的赔了钱，大不了你把车卖了，我把房卖了。”<br />
<br />
中国把贱卖群众血汗换来的巨额外汇存底，大部分买了美国债券，好贿赂西方统治阶级，别在人权问题较真。这些国债是有回扣的，像余隆这位真命皇太子，自然有花不完的钱，考不上国外音乐学院也没事，反正可以游学世界，爱当指挥当指挥，什么都该是最好的，老婆也不例外。<br />
<br />
在大内总管曾庆红的“照顾”之下，“北京国际音乐节”胜利闭幕。两个月后，余隆和曾伟坐上了飞往纽约的飞机，他们此行的目的是为了说服世界小提琴大师斯特恩参加第二届北京国际音乐节的闭幕式演出。<br />
<br />
斯特恩上一次来中国还是在1979年，这是数十年来首位到中国访问的世界级音乐大师。美国著名的纪录片导演艾伦米勒根据斯特恩随行人员拍摄的素材，剪辑出电影《从毛泽东到莫扎特》，出现在影片的许多人，20年后都和余隆的发生了联系。在中央音乐学院的大师班上亲自指点过的一位叫徐惟聆的美女，日后成了余隆的太太。<br />
<br />
他们是打着赌去结婚的。那是在美国，他们在一顿午饭之后闲逛在纽约街头，看见很多人在city hall门口排队，然后集体宣誓结婚。余隆回忆“当时我跟她说，‘哎，这挺有意思的！你敢不敢？’‘有什么不敢的。’就这样我们结婚了。从city hall出来，我们给所有在纽约的朋友打电话，说大伙一起出来吃个饭吧，因为我们结婚了。他们认识才仅仅一个月。太子党“拍婆子” 果然不同凡响。<br />
<br />
老江爱开洋荤，喜欢交响乐团，1997年去了“中国交响乐团”的首场音乐会，这是中国第一个采用西方音乐总监制的乐团，当时是陈佐煌。可惜橘逾淮而化为枳，把财政权交给了音乐家，陈佐煌挪用公款，给自己盖房子，丑闻爆发，居然全身而退。“中国交响乐团”是“中央乐团”改制的，当年余隆羽翼未丰，吃不到嘴，只能乾瞪眼。2000年另起炉灶，搞了个“中国爱乐团”，原因是听说，国外有柏林爱乐、维也纳爱乐、纽约爱乐，好像起名了“爱乐”名声就响了，就有办法了。人家国外的爱乐乐团，后面有“爱乐协会”支持，你有什么呢？<br />
<br />
余隆的后台是保利集团，也是解放军贪污军费，洗钱的白手套。大买圆明园兽首，财大气粗，宣扬爱国主义，和西方主流社会作对。保利当然也“爱乐”，为毛主席的嫡生子保驾护航，自然是解放军的光荣天职。<br />
<br />
我告诉大家一个秘密，“中国爱乐”是外国指挥在后台训练的，余隆只是摆摆样子，和团员并没有眼神交流，内行人一眼即知。保利集团花了大钱，国外乐评只是客气的“credible”而不是由衷赞叹的“incredible”。老外不买帐，皇太子余隆无法走向国际，那怎么办呢？他把脑筋动到了洋人识货的“上海交响乐团”头上。<br />
<br />
如果说北京是柏林，上海就是维也纳。上交是一个很有个性的乐团，在这里，乐团是将军，指挥是兵。开除全团，重新招考，搞得和北京一样，上交１５０年的传统毁于一旦。余隆真不愧是上海文化的天敌，左王毛泽东的最佳传人。<br />
<br />
果不其然，余隆拿下“上交”，以此为敲门砖，果然受邀指挥了“纽约爱乐”“芝加哥交响乐团”。<br />
<br />
原来我在中央音乐学院是学作曲的，后来是公费留苏最后一届，在莫斯科转行学指挥，一路拿到博士。为什么我们科班出身的，只能走穴教书，余隆只是个走读生，游学八年，连国外音乐学院都考不取，居然可以呼风唤雨。原来他是真龙太子，怎么折腾都没有关系，这就是中国大陆的现状。<br />
<br />
我十年前指挥过台北市交响乐团，演出肖斯塔高维奇第五号“革命交响曲”，在台湾有很多好朋友，爱人Cindy来自热情奔放的南台湾，深知台湾民众纯朴好客的传统。前不久，袁红兵教授新书《台湾大劫难》，指控共产党是“人类有史以来最为诡诈的政治动物”。读了这篇文章，读者朋友可以发现，在共产党的统治之下，连最纯朴的音乐界也难逃魔掌。善良的台湾同胞千万小心。<br />
<br />
（作者林涛，莫斯科音乐学院毕业，现职俄罗斯克麦罗沃交响乐团艺术总监、首席指挥）<br />
<br />
编后按：按大陆媒体介绍，近年来，林涛不但数次与中国国家交响乐团合作，在北京国家大剧院成功指挥交响音乐会，2008年在维也纳金色大厅还指挥了中国新春音乐会，2009年作为音乐总监又成功地指挥了唐建平作曲的歌剧《青春之歌》的首演。因为林涛经常中国大陆演出，所以此文也许是假托之作。刊出此文，供读者参考。<br />
