胡兵 泰国<br />
<br />
================================================================<br />
<br />
可以料想的是，这么多的明星是外国国籍，一定会引起很多的争议。很多人会觉得，为什么呢，是中国的影视，中国的观众捧红了你，你到头来怎么是外国籍呢？<br />
<br />
我倒是没有这么想，我只是不知道胡兵加入泰国国籍是怎么想的。好在三年以后，我们有胡斌，哪怕是个假的，那我们也有胡赝斌。言归正题，我们不说出国方便，不说个人自由，不说漏税避税，我认为，一个国家，能让这么多艺人转变国籍，这个国家一定是有他的责任的。在我们谈个人责任和义务的时候，我们也要谈国家责任和义务。这代人要记住的是，国家利益不是高于一切的，国家的合理利益在特定的时候才可以高于一切。<br />
<br />
这么多人跑了，说明建国以后很多的大业尚未完成，否则就是很多中国籍的外国人来参与制作或者演当时的反面角色。他们变换了国籍，是他们的选择，这个选择就像离婚一样，可能是感情破裂了，可能是遇见了更好的，在道德层面上是可以谴责的，但是在人格层面上是没有任何问题的。你也难保你们自己啊，对吧。给你美国籍，电脑前的你要不？<br />
<br />
反正我个人觉得我的国籍挺好的，也就是多交点税少享点福，出国麻烦点，其他也没什么。<br />
<br />
对于普通老百姓来说，也就是吃不起住不起玩不起结不起生不起病不起死不起，但最关键的是，还移民不起，所以，看见你们跑了，不爽是肯定的。<br />
<br />
在看得见的未来里，我是不会改变我的国籍的。但是电脑前的所有人，你们不要忘记了，这个国家给你提出了条件，你也是可以给这个国家提条件的。我的条件是，我无所谓我爱的国家对我们这个行业的从业人员的利益几乎没有保护，我也无所谓这个国家纵容随便一套房子的利润就抵上中国最大的出版社的全年总利润，但我很喜欢小孩子，我可能无法遵守计划生育，更无法接受计生委的人碰我的女人，所以，如果我不小心生多了孩子，我就不会是这个国家至少不会是大陆的国籍了。<br />
<br />
可能很多人要说，却，谁在乎你啊。话是没错，但我就更不在乎了。都互相不在乎了，这不就离了麽。你看上面名单上的那些人，还不都是人模人样的。<br />
