													<h2>任大嘴惊人语录为何惹人恼怒？</h2>
						
						
																											<div id="postmessage_367095" class="t_msgfont">近日, 任志强频出雷语，继声称“年轻人就该买不起房”后，又质问“买不起房为什么不回农村”，话语一出，又吹皱了一池春水，引来网友炮声隆隆。<br />
<br />
其实，这已经是反复重复的场景了，近年来，围绕中国的房地产市场，任志强说了不少话，吵了很多架，也获得了从“任大嘴”到“全民公敌”等诸多称号。在当前中国特有的土地供应制度下，房地产商的牛皮烘烘很难让人服气，从拿地到拆迁直到最终上市中,存在着相当多的灰色地带，这使得房地产商们天然就成为了被公众质疑的对象，在这一点上，笔者也和其他人一样，对于高调的“任大嘴”从没有什么好印象。不过，看不顺眼是一回事情，他说的话有没有道理则是另一回事情。<br />
<br />
网络上已经有编辑好的现成的“任志强惊人语录荟萃”，粗略一看，似乎可以分为几类，一是对企业自主权的坚持，如“我没有责任替穷人盖房子，房地产开发商只替富人建房 ”、“商品房定价无需被监督” 、“我是商人，不考虑穷人！”、“房价降了，房地产开发商有权不盖房”，等等，这些话听上去很刺耳，可话糙理不糙，难道企业不能自主决定其服务对象吗？难道企业对自己的产品没有定价权吗？难道企业能够亏本提供产品吗？只要将任大嘴的这类话翻译一下，就会发现,其实并没有什么好雷的，更像是一些关于市场经济的老生常谈，“买卖有理、炒房无罪，禁止炒房就是违宪”也没有什么错，产权交易本就不应该被禁止，还应该给予法律的保护才对。<br />
<br />
惊人语录中还有一类,则主要是对当代中国房地产市场现状和未来走向的一些点评，说白了就是坚持唱多，如“宏观调控不是为了让房价下跌，是为了稳涨”、“粉丝卖到鱼翅的价格才叫泡沫”、“工资在涨 GDP在涨房价不涨就不对”、“中国人太有钱，房子太便宜”，等等，俗话说逢人减寿,遇货添钱，指的是已经买到手的东西，而对于尚待购买的东西，人们更期望它跌价，因此,任志强一味唱多，对于那些尚未买房的人士来说，就不够讨喜了。可是，考虑到中国现行土地供应制度和财富分配现状的双重作用，在特定时间段内唱多，似乎也有他的道理。一方面，是政府为获取高价的土地转让收入而限制土地供应量，另一方面，是在权力主导财富分配现状下，少数人拥有了过多的财富，这样的结果只能是“20%的富人买走了大部分房产（王石）”，在特定时段内的房价又岂有不涨之理？至于说房地产最终的周期调整难免，那已经是另一个话题了。<br />
<br />
任志强惊人语录的第三类则主要围绕穷人和富人而展开，如“高档房本来就是穷人买不起的”、“我的房价不会降，让外地甚至境外富人来买房“、”过去中国都是穷人区，现在出现穷人区和富人区是很正常的，就像让一部分人先富起来一样，也要让一部分人先住进富人区，以后才能都变成富人区”等等，我注意到，任志强的惊人语录中,穷人和富人这两个名词出现频率相当之高，当然，这一频率未必是因为任志强先生使用过频,也可能是被集中搜集的结果。但是，如此频繁地使用富人和穷人这样对立的词汇，不能不说是任志强的一个特色，而在我看来，在所有三类语录中，很可能这才是任志强成为“全民公敌”的主要原因。<br />
<br />
其实，在上述三类语录中，任志强都喜欢使用穷人和富人的对比，即使是为了坚持企业自主权，强调企业应该自行决定其客户对象，他也会采用“我没有责任替穷人盖房子，房地产开发商只替富人建房”这样的张狂表达，在我看来，这样的表达所能达到的效果，在当代中国敏感的贫富分化现状下，传达出去的不可能是正确的道理，而只能是愤怒的情感。因此，在我看来，任志强惊人语录的出现，与其说是道理太悖谬，不如说是太伤人感情，以致无法为人所接受。10多年前，意大利球星维埃里以创记录的5900万美圆由马德里竞技转会到了拉齐奥，当时的教宗保罗二世，曾经的瓦多韦斯神父足球队守门员对此发表评论，认为这样高的价格是对穷人的侮辱。从道理上来说，双方你情我愿的交易，不应该侮辱和伤害到不相干的人，可感情之所以为感情，就在于它并非完全顺从逻辑和理性，而带有强烈的感性因素。<br />
<br />
让任志强成为“全民公敌”的可能不是其错误的表达，正是因为他一而再再而三地触动贫富差距这一当代中国最为敏感的神经，当他口若悬河地运用穷人和富人这样的词汇时，也就将人分成了两等，而他也毫不掩饰自己对两者的不同态度：对富人的迎合与对穷人的蔑视，或许任志强先生本人并无意冒犯谁，但是，他本人的身份和当代中国贫富分化的现实却难以为人所忽略，于是，即使他所说不过老生常谈的道理，也深深地刺激了民众，并造就了日益丰富的惊人语录目录。</div>

							
							
							
							
