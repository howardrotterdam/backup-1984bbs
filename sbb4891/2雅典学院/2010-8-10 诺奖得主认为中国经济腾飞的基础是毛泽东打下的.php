																											<div id="postmessage_777238" class="t_msgfont">诺奖得主：中国经济腾飞的基础是毛泽东打下的 <br />
2010年08月10日 11:18 南方人物周刊<br />
核心提示：这个“偶然”当然有一定的内在基础。假如说毛泽东时代中国有很不利的经验，同时它也建立了普及的教育系统，提高了人民的卫生状况，所有经济学家都认为这是经济发展的前提条件。比方说我同事阿玛蒂亚・森（1998年获诺贝尔经济学奖），论述中国和印度的发展区别时，经常强调印度不如中国的地方，一个是没有很好的基础教育系统，另一个是没有基层医疗卫生系统--而中国这两大基础是在毛泽东时代打下的，否则这30年中国经济不可能有这么大的飞跃。<br />
<br />
<br />
<br />
裴宜理 来源：南方人物周刊<br />
<br />
摘自《南方人物周刊》2010年第6期 专访哈佛教授裴宜理 本文有删节<br />
<br />
假如没有毛泽东和邓小平<br />
<br />
人物周刊：您似乎说过中国经济的成功有偶然性和不可复制性，请您做些解释。<br />
<br />
裴宜理：这个“偶然”当然有一定的内在基础。假如说毛泽东时代中国有很不利的经验，同时它也建立了普及的教育系统，提高了人民的卫生状况，所有经济学家都认为这是经济发展的前提条件。比方说我同事阿玛蒂亚・森（1998年获诺贝尔经济学奖），论述中国和印度的发展区别时，经常强调印度不如中国的地方，一个是没有很好的基础教育系统，另一个是没有基层医疗卫生系统--而中国这两大基础是在毛泽东时代打下的，否则这30年中国经济不可能有这么大的飞跃。<br />
<br />
同时，如果中国没有一个邓小平，也不会有这么快的进步。所以这跟中国领导者也有一定的关系。虽然他明确地说是在摸着石头过河，但同时他是在模仿日本、美国、台湾地区的先进经验。邓小平能动员起人民，我想跟“文革”也有一定关系--如果没有经历那么悲惨的10年，大家不可能改得那么快。<br />
<br />
各式各样的历史情况交织在一起，可以说是偶然的，也可以说是结构性的、环环相扣的。我的意思是别的国家不能看着中国模式好，马上就去模仿，这不一定是能够复制的。<br />
<br />
人物周刊：您还说过中国的政治制度和很多机制，比人们想象的有更大灵活性或活力，请具体解释一下。<br />
<br />
裴宜理：共产党领导的革命持续了30年才成功，比苏联革命长得多，毛泽东和其他领导人碰到过各式各样的问题，面临更多的挑战。他们首先在上海和另一些大城市动员人民，然后又转入农村；既要跟日本打仗，又要跟国民党打仗。所以这30年里，中国共产党发明了一些统治的手段，跟苏联是很不同的。<br />
<br />
比方说早期的领导者邓子恢在井冈山实行土地改革时发明了“试点”。我一个德国同事最近在研究这个问题，他发现在苏联没有这个词，也没有“从点到面”的方法，这是1920年代末中国共产党发明的。<br />
<br />
群众运动也是一个例子。列宁、斯大林都宣布过要搞群众运动，但毛泽东搞群众运动的方式有很强的灵活性。他是一个很特别的革命家，我和一个德国政治学家最近合编的书《毛泽东的无形之手》（Mao's invisible hand）中就分析了毛泽东的灵活的、有弹性的政治做法对当代中国的影响。我们不是完全讲好话，灵活性也带来了很多不利的后果，但我们认为中国革命的历史传统，带来了一种比较具有活力、务实的政治方式，这份遗产决定了当代中国的政策是怎样构思、怎样决定、怎样实施的。<br />
<br />
跟其他共产主义国家相比，中国共产党的治理时间已经相当长了。它的许多制度都继承了一部分传统，不断更新以适应新的需求。看得更长远一些，它如何应对大量经济的、政治的、社会的挑战，如何设法继续生存下去，这是我们应该探索并给出答案的。<br />
<br />
人物周刊：您怎么看胡温政府的亲民姿态？<br />
<br />
裴宜理：我觉得这很重要。中央官员不同情底层人员的困难，对国家和社会的关系会引起坏结果。这当然只是一个表面现象。同时我觉得他们主张建设社会主义新农村也有些好处，当然实行时也会发生很多问题、矛盾。但那个项目的内容也包括提高农村的普及教育、医疗，各式各样的工程。这个我完全同意，尤其那些比较落后的农业地区，不搞一些这一类的工程，他们永远翻不了身。他们试图改革农村医疗，我也非常赞成。<br />
<br />
人物周刊：奥巴马在圣诞节也到普通人家里去吃饭吗？<br />
<br />
裴宜理：奥巴马当候选人时几乎每天跑到老百姓家去吃饭。克林顿也如此，跑到工人家里和他们一起喝啤酒，说我就是一个普通百姓。<br />
<br />
人物周刊：对中国的政治家来说，儒家文化中有哪些遗产是今天可以利用的？<br />
<br />
裴宜理：回答这个问题我同事杜维明应该是最佳人选，他最近在北大建立了一个高级人文研究中心。我觉得孟子的民本思想值得重视，“民怒弥天”、“天下可忧在民穷，天下可畏在民怒”，这些都是中国很宝贵的传统，我赞成中国人重视儒家思想。</div>

							
							
							
							
