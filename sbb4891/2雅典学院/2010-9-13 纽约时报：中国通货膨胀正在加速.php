By KEITH BRADSHER<br />
2010年9月11日发布<br />
对于林先生（K. K. Lam）这样一位在广州担任会计师工作的香港人来讲，通货膨胀意味着猪肉和白菜等蔬菜的价格更高了。<br />
<br />
对于董先生（Allen Dong）这样一位来自宁波东北700英里一家家庭用具制造厂的销售经理来讲，通货膨胀意味着他需要说服他的零售商们为他的抽湿机产品付更多的钱，来保证他的公司可以应付工资和原料价格上涨。<br />
<br />
从街头市场到公司办公室，消费者和高管们都在想办法应对上涨的物价。国家统计局周六宣布消费价格指数比上年同期增长3.5个百分点，这是近两年来的最大涨幅。<br />
<br />
更严重的是，通货膨胀在短期看来正在加速。通过对8月份同7月份的价格比较后发现，通货膨胀率接近了每年4.8个百分点。<br />
<br />
中国的物价上涨让美国和欧洲人嫉妒不已。，上周六发布的其他数据还体现了经济在增长，产品供应充足，银行在积极放贷。与去年8月份的数据相比，工业增加值增长了13.9个百分点，零售额增长了18.4个百分点，银行贷款增长18.6个百分点，固定资产投资增长24个百分点。<br />
<br />
这四个方面的增长都好过了之前经济学家的预期，中国经济保持活力的同时，其他地方的复苏却正在减缓。<br />
<br />
另外一则由海关总署在上周五发布的数据表明，中国对进口产品的需求依然强劲。上个月的贸易顺差缩小到200亿美元，如果除去与美国的180亿美元的顺差，贸易收支几乎平衡。<br />
<br />
但是在中国太多的现金去追逐有限的货物推高了物价。通货膨胀开始带来问题，尤其对于那些刚刚开始进入劳动力市场的年轻人和只有固定收入的退休者。<br />
<br />
技工学校毕业的年轻人在沿海可以拿到200到300美元的工资，一些在内地的则更少。增长的物价促使他们寻求更高的工资收入，蓝领阶层的工资增长倒比通货膨胀更快。<br />
<br />
但是大学应届毕业生在沿海地区的300到500美元工资在过去的几年却是下降的，尤其在没有对通货膨胀做出工资调整之前。过去十年间中国大学的大量扩招导致了毕业生数量大大超出了企业的可用工数量，除了低薪就职别无他法。<br />
<br />
和其他的许多国家一样，退休者是最容易受到通货膨胀攻击的人。林先生说他的母亲一个月的退休金只有150美元。<br />
<br />
工资的增长导致企业不得不提高他们的产品价格。宁波德业（家用电器）科技集团销售经理董先生说，工资不得不每年提高10%的工资，同时，原材料的价格也在不断上涨。<br />
<br />
“不能把我们的成本压力全部转嫁到我们的客户身上，因为这样做的话，他们都会跑掉的，”他说，“我们现在正在对我们的组装生产线做一些研究看能不能做出一些改变来提高效率。”<br />
<br />
但是从上个月的固定资产投资的强势增长来看，中国企业在物价不断上涨面前依然在建设更多的工厂、办公大楼和其他的设备，而不是减少。<br />
<br />
汕头一家以制造书包和小型计算器企业――坤宁公司的销售经理潘宁说，虽然劳动力和原材料的价格已经上涨了5到10个百分点，但是随着北半球的新学年开始，他们已经开始提高价格来应对成本的上涨。<br />
<br />
中国官员说了很多年，他们不能接受超过5%的通货膨胀速度，每当通货膨胀接近那个水平的时候，他们就会施压于银行贷款和固定投资。他们已经在近来几个月采取了一些措施，但是最近又有所放松，因为经济学家提出国内需求或许并不像8月份的数据所反映的那样强劲。<br />
<br />
现在，每天都在上涨的日常消费品价格使中国消费者苦恼不已。<br />
<br />
“坦率的讲，我不明白年轻人该如何处理好自己的工作，” 林女士说。“他们几乎用自己一般的薪水去和别人同租一个环境很差的公寓，如果你增加了食品和交通方面的开支的话，那么就将没有任何多余的钱可以送回家了。”<br />
<br />
（Hilda Wang来自香港的报道）</div>

							
							
							
							
