To make matters worse, inflation overthe short term also seems to be accelerating. A seasonally adjusted comparisonof August prices to July prices showed that inflation was running at anannualized pace closer to 4.8 percent. <br />
<br />
更糟糕的是，超越短期的通胀也肯能被加速，从八月对照七月的季节性调节看通胀正在以年4.8个百分点前进。<br />
<br />
Prices are rising in China forreasons that many Americans or Europeans might envy. The economy is growing,stores are full and banks are lending lots of money, according to otherstatistics released by the government on Saturday. Compared with August of lastyear, industrial production rose 13.9 percent last month, retail salesincreased 18.4 percent, bank lending climbed 18.6 percent and fixed-assetinvestment surged 24 percent. <br />
<br />
中国价格的增长似乎是许多欧美人妒忌的理由。经济的增长，库存的充盈以及银行放贷的增加，周六政府部分统计学家的公布，比较去年八月份，工业生产总值比较上月增加百分之13.9，零售增加18.4，银行贷款攀升十八点六个百分点，固定资产投资增加二十四个百分点。<br />
<br />
 <br />
<br />
 <br />
<br />
All four categories rose slightlymore than economists had expected, in the latest sign of the Chinese economy’sstrength even as recoveries seem to be flagging elsewhere. <br />
<br />
以上四项指标轻微的增长超过了经济学家的预计，最近中国经济的强劲甚至可视为一种恢复低垂经济的拐点。<br />
<br />
Separate data released on Fridayby the General Administration of Customs showed that Chinese demand for importsalso remained surprisingly strong. The trade surplus narrowed to $20 billionlast month, and would have been nearly in balance without China’s $18 billion surplus with the United States. <br />
<br />
中国海关署在周五单独公布的数据显示中国的进口需求也保持着惊人的强劲。贸易顺差上个月缩小到二百亿美元，并且似乎超出中美贸易逆差一百八十亿美元的平衡线。<br />
<br />
But so much cash in the Chineseeconomy chasing a limited volume of goods is pushing up prices. Inflation isstarting to become troublesome, especially for young people entering the workforce and retirees on fixed incomes.<br />
<br />
但是在中国如此之多的现钞正在追逐着上涨的价格。通胀正变得很棘手，特别对于刚刚进入工作的年轻人和靠固定收入的退休人员。 <br />
<br />
Young people with vocationalschool degrees typically earn $200 to $300 a month in factories near the coastthese days, and somewhat less in the Chinese interior. Rising prices haveprompted many to ask for bigger paychecks, and blue-collar incomes haveincreased faster than inflation.<br />
<br />
沿海地区的职业学校毕业生每月的工资大多是二百美元到三百美元。这些年，这些钱在中国内地是很微薄的。物价上涨预示着提高工资，比较于通胀蓝领的收入一直在很快的增长。<br />
<br />
But salaries for recent collegegraduates, at $300 to $500 a month in coastal areas, have actually declined inthe last few years, even before adjusting for inflation. A rapid expansion ofuniversities over the last decade has resulted in more young men and women withundergraduate degrees than companies are ready to hire, except at lower pay.<br />
<br />
但是大学毕业生的工资在沿海地区是三百到五百美元，事实上比以往几年降低了，甚至低于调节通胀前。快速的大学扩招导致很多年轻人拿到了学位。而公司除了闲杂低收入岗位外没有这么多的岗位提供给他们。<br />
<br />
And as in many countries, retireesare among the most vulnerable to inflation. Ms. Lam said her own mother livedon a pension of just $150 a month.<br />
<br />
MRS LAM说在退休人员易于受到通胀打击，她的母亲仅仅能拿到150美元/月的退休金。<br />
<br />
Rising wages are putting pressureon companies to increase their prices. Mr. Dong, the sales manager at theNingbo Deye Domestic Electrical Appliance Technology Company, said the companyhad to raise wages by 10 percent a year, while raw material costs were alsoclimbing.<br />
<br />
宁波德业电气销售经理Dong先生称，增加工资就要增加他们产品的价格，公司每年必须增加百分之十的工资比例，其间原材料的价格在不断的上涨。<br />
<br />
 <br />
<br />
“It is impossible to transfer ourcost increases entirely to our customers, because if we do so, they will allrun away,” he said. “We are currently doing a study of our assembly line workprocesses to see where we can achieve greater efficiency.”<br />
<br />
将价格转嫁给消费者是不可能的事情，因为如果我们这么做他们就会转头就走。我们现在可以做的就是研究组装生产线目来提高生产率。<br />
<br />
But as the powerful growth infixed-asset investment last month showed, Chinese companies are stillresponding to rising prices by building more factories, office buildings andother equipment, instead of cutting back.<br />
<br />
但是对照上个月强劲的固定资产投入，中国的公司仍然在响应高涨价，通过建设更多的工厂，办公大楼和添置其他设备，而不是削减这些基础设施投入。<br />
<br />
Pan Ning, the sales manager at theNewsunda Industries Company, a manufacturer of school bags and pocketcalculators based in Shantou,said labor and raw material prices had been climbing by 5 to 10 percent. But asschool years have begun around the world in the Northern Hemisphere, Newsundahas been able to raise the prices it charges to cover the increased costs, Ms.Pan said.<br />
<br />
PANNING，NEWSSUNDA，位于汕头的一所制造书包和计算器的产业公司的销售经理。她称，劳资和原材料的价格已经攀升五到十个百分点。但北半球的新学年已经开始。NEWSUNDA已经增加产品价格来冲销那部分增长的包装成本。<br />
<br />
Chinese officials have said formany years that they regard 5 percent inflation as unacceptable, and they haveshown a willingness to clamp down on bank lending and investment wheneverannual increases come close to that level. They have taken some of these stepsin recent months, but more recently eased back on lending controls as someChinese economists suggested that domestic demand might not be as strong as theAugust data showed.<br />
<br />
中国官方不承认五个百分点的通胀率，并且每当达到这一水平他们乐于压制银行贷款和投资。他们在最近几个月逐步实行。但他们又回软他们的控制，原因由于中国的经济学者暗示内需似乎没有八月份显示数据那样强。<br />
<br />
For now, many Chinese consumersare irked by rising prices for everyday necessities.<br />
<br />
直到现在，大多数中国消费者被每天必需品上涨的价格所苦恼。<br />
<br />
“I honestly don’t know how youngpeople starting out in the work world manage,” Ms. Lam said. They pay nearlyhalf their salaries for their own room in a shared apartment in a badneighborhood, she said, “and if you add in food and transportation, there willbe nothing extra left in your salary to send home.”<br />
<br />
我实在真的不知道年轻人将如何开始打理他们的工作，MSLAM说，他们要支付一半的工资为他们拙劣的房子，如果增加食物和交通成本，他们的公司将所剩无几。<br />
<br />
Hilda Wang contributed reportingfrom Hong Kong.</div>

							
							
							
							
