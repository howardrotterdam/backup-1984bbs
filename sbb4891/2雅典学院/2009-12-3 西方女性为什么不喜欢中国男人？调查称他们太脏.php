　　这在17世纪并不离奇。当时人们在大街上“方便”，在公园的小径上，在圣坛上。人们踩着粪便走路，既有狗的粪便，也有贵夫人、王公贵族的，很多。为了盖过粪便的气味，人们拼命搽香水。人们还从不洗澡。国王一生就洗两次澡。在脏兮兮的身上穿上精心缝制的锦衣绣袍。人人都散发着臭气。所有的人都发臭。<br />
　　“法国人不喜欢洗澡，国王路易十五一生才洗了三次澡，一是诞生时，二是大婚日，三则是入殓。因为不爱洗澡，所以法国人才潜心研究香水。”<br />
&nbsp; &nbsp; 太阳王本人极不喜欢洗澡,经常数月不洗澡,只好喷大量香水掩盖体味.国王不常洗澡,大臣们也不敢天天干净,于是上行下效,极大的推动了法国的香水业发展。</div>

							
							
							
							
