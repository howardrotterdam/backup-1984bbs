													<h2>西方女性为什么不喜欢中国男人？调查称他们太脏</h2>
						
						
																											<div id="postmessage_353671" class="t_msgfont">一本名为《中国男人调查》的图书试图全面分析和回答这些问题，最终得出了这样一个令人震惊的诊断结论:中国男人患上了集体精神阳痿症。这真的是事实吗？又或许，这只是属于张结海的样本调查，一个片面的真相？<br />
<br />
西方人“以貌取人”<br />
<br />
“看了开篇那个报告之后你就不会有这样的疑问了。事实上在做这个报告之前，我们也没有事先得出一个结论然后再来做调查。”本书作者张结海断然否定了记者关于得出“精神阳痿”这样的结论是主题先行的质疑。<br />
<br />
主持“中国男人调查”这一课题的是曾经因“流氓外教事件”而名声大噪的张结海教授。近年来，张结海教授以“我愤怒”的形象出现在公众面前，一再成为媒体关注的焦点，这一次，这样尖锐的调查结果恐怕又将引起轩然大波。<br />
<br />
从2006年3月开始，前前后后差不多有两年，张结海和他的团队专注在“中国男人调查”的课题之中。“我们做这个课题出发点是一个判断”，张结海教授对记者说明他们研究这一课题的初衷，“我们的改革开放，一是对内改革，二是对外开放。毫无疑问，中国对外开放的程度越来越高。对外开放的重点其实是对西方开放，因为西方的现代化程度比我们要来得高。这个过程中，有两个问题我们一直没解决，一是崇洋媚外，二是西方对中国的误解。”<br />
<br />
这次调查，一部分是面访，采访对象除三位为拥有西方女友的中国男性外，其余都是西方女性。最后他们精选了其中的一部分(共15份)，收录在《中国男人调查》一书中。“男性方面，我们做了三个与西方女性有过性关系(或者是女友，或者是妻子)的调查。这样的中国男性本来就少，我们发动了很多社会力量，最后只找到三个。”对于调查中中国男性方面人数稀少的问题，张结海如是解释。<br />
<br />
除了面访，调查小组还做了108份有效问卷的问卷调查。对于女性调查对象，他们的标准是三个必要的条件:发达国家的女性、白人、在中国生活过。调查小组在大洋洲选了澳大利亚和新西兰，美洲选了美国和加拿大，北欧的瑞典、芬兰、挪威，西欧的爱尔兰、英国、法国、意大利、德国、西班牙、希腊作为样本国家，排除了东欧等经济较为落后的前社会主义国家。<br />
<br />
在这些发达国家女性的眼中，中国男人的形象究竟是怎么样的呢？从“西方女人眼中的中国男人6大缺点排名”中我们也可以很清楚地看到中国男人的问题所在，排在第一位的是“缺少教养、没有绅士风度、不文明、不礼貌”，排在第二位的则是“随地吐痰、脏、不注意个人卫生”。一位法国女人的访谈很有代表性，这篇访谈的标题就是“中国男人实在太脏了”。她说中国男人不仅外表不注重修饰，不注意保养和锻炼，最要命的是，他们还随地吐痰，这在西方是最最没有教养的表现。<br />
<br />
“这个结果我也很意外。”张结海说他一开始听到她们的评价也吃了一惊，“为了确认这一点，我们还专门问她们，你们说的是不是农民工？她们说不是，是公司里的同事，也没有达到她们对干净的最低要求。我们在做试调查的时候，就有人提出，我们做的是中国城市男人还是中国农村男人调查，我们这次就明确提出来，我们做的是中国城市男人调查，不包括中国农村男人。”<br />
<br />
调查的另一个意外结果是，中国男人对西方女人的吸引力和西方女人在中国生活的长短没有任何关系。因为中国男人外表上的问题，第一眼就阻止了她们对中国男人的兴趣，这就使得中国男人内在的品质西方女人根本就无法了解。<br />
<br />
谈到这个问题，张结海对记者强调说:“我们做调查之前犯了一个错误，我们以为外国女人不喜欢中国男人可能是因为中国某些内在的品质和她们的文化背景有差异，所以设计了很多这方面的问题，后来才知道根本不是这样。”他们采访后发现，尽管这些西方女人来自不同的国家和大洲，可是对中国男性的认识却惊人地相似。然后他们把问卷作了改动，变为一半考察内在，一半考察外表。他感慨地说，西方人对外表之重视，远远超过他们的预料之外。<br />
<br />
中国与西方的关系<br />
<br />
“这个课题，看上去是做男人女人两性关系，其实背后支撑着它的基本着眼点就是中国与西方的关系、中国人和西方人的关系。”张结海强调他们的调查并不停留在表面上，“我们以前做东西方的差异比较，多是比较印象式的，我印象中还从来没有人做具体的东西方形象比较。通过调查，我们可以得出这样的结论:中国人缺乏自信心。崇洋媚外也好，自信心不足也好，这是个老话题，为了将这个老话题做出点新意，我们考虑后决定从中国男人调查入手。通过中国男人和西方女人这个视角，来折射中国人自信心的问题，我们也想找找原因。”<br />
<br />
张结海承认，东西方互相的误解有文化差异的因素在内。中国男人总体来说是内秀型的男人，中国文化本身就注重内涵。张结海进而根据外表和内在将中国男人分成四类。第一类为外表内在俱佳；二是外表好，内在不好；三是外表不好，内在好；四是外表内在都很差。他发现，中国人排序，排在第一位的往往是外表不好内在好，排在最后的是外表好内在不好，因为中国人非常憎恶金玉其外败絮其中的那一类男人，认为他们像陈世美那样无可救药，甚至比那些一眼就能看穿是坏蛋的人更有魅惑力，所以危险性也更大。中国人总是这样小心谨慎，但这个排序和西方完全不同，西方人排在第一位的是外表内在俱佳，排在最后的是外表内在俱差，他们的想法很简单，:外表好内在不好的人至少还有一点外表好。<br />
<br />
“我跟西方女性解释了之后，她们回答我说:如果我不从外表判断一个男人，那么请问我从什么地方来判断一个男人？有一位西方女性叫安娜，也是我的同事，在中国生活了十年，我对她说，在中国，如果在一个事业单位中，一位男性打扮得太漂亮了，人们就会怀疑他的能力。她听了之后感到非常惊讶。”张结海说。<br />
<br />
这是东西方文化的差异造成的。<br />
<br />
张结海用了三个指标来调查中国男人对西方女人的吸引力。<br />
<br />
第一个指标是你在中国期间发现有多少中国男人对你有吸引力？平均是1.6。<br />
<br />
第二个指标是你在中国期间和多少个中国男人约会过？平均是0.6。<br />
<br />
第三个指标是如果一个中国男人和一个西方女人约会，他成功的几率是多少？平均是35%。<br />
<br />
张结海认为，这三个指标得出的结论是中国男人对西方女人的吸引力比较低。<br />
<br />
从“中国男人与西方男人在40个维度上的得分平均值的比较”中我们也可以发现，中国男人在男性气概、敢作敢为、举止得体、礼貌、浪漫等指标上都落后于西方男人，唯一让人意外的是，中国男人比西方男人更“雄心勃勃”。<br />
<br />
影视的影响<br />
<br />
那么我们是否一定要按照西方女性的标准来看待我们自己？面对记者的质疑，张结海反驳说:“如果我们把这个问题说极端一点，现在中国还有自己的标准吗？除了中医或中餐之外，中国人还有自己的标准吗？当然这话比较极端。我们现在的进步主要是西方带来的进步，如果我们把生活中的西方元素都抽掉的话，我们要倒退多少年？至少一百年。相反，如果我们到西方把中国的元素都抽掉，他们的生活基本没有什么影响。我们心理学领域里，基本上看不到一个中国人的名字。现在的中国男人都是一个西方男人的样子。发型我们以前不是这样的发型，眼镜也不是中国人发明的，衣服也不是中国传统的汉服或马褂。从衣服、裤子到眼镜、发型，从头到脚，你找不到一点中国本身的传统。所以，你不得不承认，现在，西方的文明的标准已经代表了世界最高标准。这个你回避不了。”<br />
<br />
从这个意义上来说，我们也许能比较理性地看待这个问题。不过，在采访过程中，张结海一再向记者强调，影视作品对于中国男人的形象产生了深刻的影响。有外国女性对他说，她们觉得中国男人很内向很害羞，在性行为方面并不在行，但后来她马上发现自己的这个论断是草率的。“不然中国怎么可能成为世界上人口最多的国家呢？”她说。她仔细追究自己的这个论断的源头，觉得自己可能是受到了影视的影响。在好莱坞，成龙电影的海报两边都是站着男人，而007的两边总是站着女人，在电影中，中国男人似乎总是不近女色――另一个极端的例子是害羞的李连杰。而我们看到的西方电影中，西方女性似乎都是与性有关的，不论是《本能》还是007中随意和007上床的邦女郎，西方女性似乎都是令人害怕的性玩偶，这就给中国男人一种印象:西方女人性欲很旺盛。“实际上两个都不是真的。只是大众传媒的影响力实在太大。”张结海总结说。<br />
<br />
发现这个问题之后，调查小组也试图来解决这个问题。于是他们提出中国男人的四个定位，希望中国男人能朝着这个方向努力迈进，这个目标似乎任重道远:一，好丈夫；二，智者；三，热情友好的朋友，四，新侠客。 <br />
