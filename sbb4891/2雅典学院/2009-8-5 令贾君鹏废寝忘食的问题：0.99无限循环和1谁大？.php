																											<div id="postmessage_191641" class="t_msgfont">两千网友激辩0.99无限循环比1大<br />
<a href="http://www.cnbeta.com/articles/90020.htm" target="_blank">http://www.cnbeta.com/articles/90020.htm</a><br />
<br />
“0.99999无限循环和1究竟哪个大?”这个问题可把做家教的西南大学研二学生朱小勇给难住了.网友们对这一问题更是各执一词,等于、大于、小于三种答案众说纷纭.<br />
<br />
昨日,记者采访重庆一中的邹发明老师,邹老师说:“教材上用等号表示,但这个等号不是绝对意义下的等.<br />
<br />
网友1大的提法是错误的,没有这种比较.”但重师数计学院刘凯年教授则表示,严格说来就是等于1,不管是极限还是普通意义上.更有教授提出了虽然是极限下的等于,如果要比较应该是1大.<br />
<br />
初中生发问难住研究生<br />
<br />
暑期做家教的西南大学研二学生朱小勇最近遇到了一个难题,在北碚上初中的学生问了他这样一个问题,“0.99999无限循环和1究竟哪个大?”他一开始不假思索地回答:“肯定是1大.”结果这个学生告诉他:“有老师说是一样大.”<br />
<br />
这让当家教老师的他很没面子.究竟哪个更大?朱小勇告诉记者,他已经将题发到大渝论坛,但网站网友的说法不一,大于、小于、等于的观点都有,网友对每个答案给出的理由又很充分,这让他犯了难,“究竟我该听谁的好?”朱小勇希望记者能够帮他咨询.<br />
<br />
网友3种答案谁对谁错?<br />
<br />
昨日,记者网上搜索,发现这个问题同样被网友关注,其中天涯上,该帖成为置顶热帖,昨晚记者截稿时粗略统计,共有2033名网友参与讨论,129名网友回帖.网友们的说法正如朱小勇所描述的那样五花八门.<br />
<br />
“好简单嘛,明显是1大撒.”网名为“红烧叮叮猫”的网友回答.他给出了1大的理由, 0.999……,无论怎么循环,只是无限趋近于1,无论怎么趋近,还是没到1,所以1大,在天涯上支持“红烧叮叮猫”1大的网友占42%.<br />
<br />
“是等于的撒,可以算出来嘛.”接近58%的网友给出了这种答案,网友“海月朵朵”还给出了证明解法:0.99・・・=0.33・・・×3=1/3×3=1,一名小学生更是打包票说:“五年级的课本老师都教过了,就是等于”.<br />
<br />
还有一个网友别出心裁地给出“0.99999无限循环比1大”的答案,但并没有给出证明依据,所以无人响应.究竟谁大谁小?看来在这个问题上,网友也意见不一,那么专家呢?<br />
<br />
数学专家说法不一<br />
<br />
昨日记者请教重庆一中数学教师邹发明,邹老师表示,网友们不能用有限的视角去看待无限的问题,这样肯定会出现偏差.<br />
<br />
0.9999的无限循环是“要多近就有多近”的意思,是一种玄乎语言.他称,不是绝对意义的等于,而是极限含义下的“等于”,这个“等于”是无限趋近的意思.<br />
<br />
而重师数学统计学院的刘凯年教授则认为,就是严格意义上的等,他打了个比方,如果不等于,你能够举出1和0.9的无限循环的差距吗?不能的话说明就是严格意义上相等的.<br />
<br />
而重庆某高校的数分组的组长则表示,极限意义下的等于,非要比较大小应该是1大.专家不同的观点让记者也产生疑惑了,究竟谁对,谁错?记者 汪再兴<br />
<br />
网友的另类解释<br />
<br />
在网上,网友们还给出了多种另类解释让人忍俊不禁.<br />
<br />
1.本来该发100元工资,却发了99块99999……,老板说是一样的,你心里爽不爽?<br />
<br />
