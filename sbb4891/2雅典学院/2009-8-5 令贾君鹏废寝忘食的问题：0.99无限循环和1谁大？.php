4.看了上面的回帖感觉明白了一个道理……为什么那些卖衣服的标价啊……电脑标价啊,一般都标价199、1999而不标200、2000……<br />
<br />
5.同学甲:0.999999无限循环与1一样大!<br />
<br />
同学乙:我们可以把数学上的假设问题换作生活问题,比如:从头上拔掉一根头发后,和原来的头发比一样多,反正也看不出来嘛(无限循环的概念可以理解为小数点后面的9多到看不见).可以这样作比较吗?<br />
