													<h2>八九民运在中国失败的另类原因</h2>
						
						
																											<div id="postmessage_112738" class="t_msgfont">作者 : 曾节明, <br />
l表rg：4/1/2009 <br />
　“陆肆”大屠杀已经过去二十年了，“陆肆”悲剧深刻地改变了中国的命运，把当时已经迫近光明井口的中国，再次重重地踹落黑暗的深渊。二十年来，对于中国八九民运失败的原因，一直众说纷纭、莫衷一是、争吵不休，近年来，终于有敏锐如阮敏先生者，正确意识到：赵紫阳在关键时刻的撒手退缩，是中国八九民运失败的主要原因――即：赵紫阳在1989年五月中旬，没有抓住学运高涨、军队动摇的千载难逢时机，站出来号召军队。 <br />
　客观的说，这确实是民运失败的最主要的直接原因，因为当时身为总书记和军委副主席的赵紫阳撒手退缩行为，固然可以洁身自好，但却等于拱手把数万学生市民的性命交到中共顽固派这头恶龙的口中，赵紫阳的行为构成了失职。 <br />
　但是，如果把中国八九民运的失败完全归咎于赵紫阳，又怎么解释捷克、保加利亚、东德、罗马尼亚等顽固派当权的前共产国家民运在1989年的胜利？当年这些国家，不要说没有戈尔巴乔夫、叶利钦，连胡耀邦、赵紫阳也没有。 <br />
　赵紫阳关键时刻的退缩，显然只是中国八九民运失败的表面原因，或者说，是深层原因的一个结果。 <br />
　虽然没有开明领导人，但当年这些东欧前共产国家的有一个共性，就是不能成功地以开枪屠民的方式镇压示威游行：面对全国性的游行示威，以雅克什为首的捷共统治集团选择了集体辞职、东德昂纳克选择了两德统一、保共总书记日夫科夫则主动下台…他们都宁可失去权力，也下不了向本国同胞开枪的狠心；当年的东欧共产国家中，唯有罗马尼亚的齐奥塞斯库试图以中共大屠杀的方式，扼杀本国的民主运动，但是，他的凶残努力迅即招致了粉身碎骨的惨败：齐奥塞斯库对本国示威民众的血腥枪杀引发了体制内的强烈反弹，这种反弹在齐氏派内务警察处决抗命的国防部长古萨之后，激变为高层将领的集体倒戈相向，国防军的倒戈，导致罗马尼亚共产党政权在四天内垮台。 <br />
　相比之下：中共顽固派对本国同胞的“陆肆”大屠杀，其疯狂和凶残，比齐奥塞斯库当年的屠杀有过之而无不及，却没有引发任何体制内的公开反叛。 <br />
　与当年的这些东欧国家构成特别鲜明对比的是：同样是顽固派，中共的顽固派在权力受到挑战时毫不犹豫地选择了开枪，邓小平早在八六年就要求：对学潮要采取专政手段，八九年更是扬言，平息动乱要“不怕流血、不怕骂娘、不怕制裁”1，陈云当时也杀气腾腾地叫嚣：“退，就是承认他们那些所谓民主选举的非法组织，承认搞资产阶级自由化，承认和平演变，等于否定中国共产党，把我们几十年战争、成千上万革命烈士的鲜血换来的成果统统毁于一旦！2，中共顽固决策开枪屠民时的坚定、冷酷、狠毒、团结，足以令最冷血的苏、东共产顽固派望尘莫及，只有满清入关屠杀汉人、日本人屠杀中国人，才能与之相比。 <br />
　可见，顽固派道德素养上的重大差别，是造成八九民运在东欧国家和中国一成一败的更深层次的原因。八九年春夏之交的中国局面，如果换到东欧任何一个国家，顽固派都必然垮台，不管有没有赵紫阳式的人物、不管赵紫阳式的人物能不能站出来。而在中国，由于顽固派远比欧洲的顽固派厚黑和不择手段，因此除非赵紫阳关键时刻敢于向邓小平、陈云、王震一伙下狠手，否则即使赵紫阳敢于站出来，民运也不大可能成功，而赵紫阳恰恰是一个宋襄公式的“仁义”政客。 <br />
　同样是共产顽固派，为什么中共顽固派敢于向本国示威民众开枪、而东欧顽固派却不能、且行不通呢？中国和东欧共产顽固派道德素养上的重大差别，是由中国和东欧国家不同的文化传统造就的。 <br />
　中国是儒家传统国家，而东欧国家都是基督教（东正教）传统国家；不同的文化传统，造成了对待人命的显著差异：东欧民族较珍视人命，东欧民族这种珍视人命的传统，并不因各国马列政党的上台而消灭；而中国民族则向来漠视人命，如曾国藩、李鸿章对太平天国统治区民众的滥杀、国民党为了打击日军不惜掘开黄河大堤，淹死中国老百姓数十万…中共的上台，只是使得这种漠视人命的观念雪上加霜而已：同样是共产头子，唯有毛泽东敢于在莫斯科共产国家会议上豪言不怕美国打核战争，“中国有六亿人口，死了四亿还有两亿，还可以打游击”3，此言一出，国际共产阵营众寡头不仅瞠目结舌、面面相觑，因为东欧当时最铁杆的斯大林主义主义者，即使残酷如海因里希、霍查之流，也不敢有如此粪土人命的豪情壮志；即便斯大林在世时，也没有过此草菅人命的大气魄。 <br />
　为什么中国和东欧民族在对待人命上会有如此显著的差异？这是因为中国和东欧国家不同的文化传统，造成了民族价值观的深刻差异。在基督教（东正教）的影响下，东欧民族多少具备西欧民族那种罪恶感、人道传统和个人至上精神。 <br />
　而中国民族在儒家的主导性影响下，没有人道传统，也不可能产生人道传统。儒家没有超越世俗权力的价值追求，没有彼岸世界的概念，因此儒家也就不承认因果报应和转世轮回、没有罪与罚的信念；没有罪与罚的信念也就没有罪恶感，没有罪恶感有两个后果： <br />
　一是道德难生根，很容易因为世俗利害关系放弃原则，这就是历来儒家信徒普遍假仁假义的根本原因，这也是成长于儒家传统社会的中国人普遍比较虚伪的根本原因，共产党的上台则是大大助长了这种虚伪。 <br />
　二是缺乏自省和忏悔精神。因为没有罪恶感，中国人非常不习惯于查找自己身上的恶，而每每以放大镜甚至显微镜追查别人身上的恶，并力求“除恶务尽”；由于没有罪恶感，中国人很少认识自己身上的恶，而且普遍觉得承认一己之恶很没面子，因此中国人缺乏自我束恶的行为习惯：　中国人每每自认为在正大光明地“揭批”、严惩、报复别人的恶，却对自己行为的穷凶极恶浑然不觉：中国的父母们常常对自己的小孩恶狠狠地棍棒拳脚相向，口称小孩不肖、“不打不成材”，却不觉得自己行为是更大的恶。 <br />
　缺乏自我束恶的行为习惯，也就没有人道的行为习惯。一方面，中国的富人们历来为富不仁，鲜有绅士作风和慈善济世行为、中国的官府则是怙恶不悛、顽冥不灵；另一方面，底层的草根大众仇官仇富也往往达到食肉寝皮的程度，普遍认为贪官奸商“不杀不足以平民愤”（而不管其罪是否该死），其毫无人道的残酷心态，与迫害压榨他们的官僚往往如出一辙。 <br />
　这样一个没有人道的行为习惯的社会，是不可能产生人道传统的。所以两千年来，中国社会的各种惨无人道的酷刑牢居世界前列，儒家不仅不反对酷刑，反而主张“乱世用重典”，随着儒家影响的加深，凌迟（千刀万剐）这种惨绝人寰世界头号酷刑的成为中国明正典刑，中国社会不是人道了，而是更残忍了。在近千年的时间里，对于缠足这种有违人道的陋习和凌迟酷刑，中国社会几乎没有抗议质疑声音，这突出地反映了中国文化传统无人道的一面。 <br />
　在这种无人道传统的影响下，中国社会对动物更是赤裸裸地显现出毫无怜悯之心：上世纪二十年代，英国思想家罗素笔下的中国民众，看见一只狗被汽车轧断腿而开心大笑，中国人的这种心情曾使得罗素困惑不已；2005年夏末的一天，相同的一幕我在桂林东环路亲眼所见。中国人的这种残酷心态是西方人难以理解的，它至今丝毫未变，这种心态就是典型的缺乏人道的心态。 <br />
　缺乏自省和忏悔精神，也就缺少歉疚之心。许多人苦大仇深地控诉自己在文革中遭受的迫害，却对自己在文革中同样迫害他人的罪恶讳莫如深，或者统统推给“上级”，仿佛个个都是天真无邪的童子；在“武斗”中曾经把“反革命”开膛破肚的那个广西凶手谈及当年的行凶居然面无愧色，从从容容地把自己犯下的发指罪恶全部推给“时代”、推给“毛主席”！总之，中国人在揭、批别人的时候总是积极雀跃，但在需要承担责任之际却每每面无愧色、堂而皇之地诿过卸责，缺乏羞耻之心和自责道德勇气。 <br />
　统治集团缺少歉疚之心，就会毫无长进、重新犯罪。邓小平既是毛泽东的帮凶、又是毛共暴政的受害者之一，但他一旦得势，就好了伤疤忘了痛，对老百姓厉行“严打”、“计生”暴政，并且对学生市民实施铁血专政，制造“陆肆”惨案。 <br />
　虽然儒家也有爱人的精神成份，但儒家之爱，局限于个人的家人和亲族，并不是博爱；儒家不仅不主张博爱，还激烈地反对墨家的“兼爱”精神。因此，儒家传统的中国社会，也是一种缺乏博爱精神的社会。 <br />
　在这种传统的影响下，邓小平、陈云、王震们对自己儿子的疼爱，毫不妨碍他们残杀别人儿子们的狠心。 <br />
　因此，中共的领导人在作屠杀本国同胞决策的时候，要远比东欧的同行们更少心理包袱。 <br />
　同样是共产党党棍，二十年前，面对数以万计的游行示威民众，前东欧共产国家的顽固派们犹豫了、动摇了、酗酒了、分化了、辞职了、倒戈了…除了齐奥塞斯库短暂的疯狂外，他们的手一时间都沉重麻痹无比，始终扣不动屠杀的扳机，结果，这些国家八九民主运动都成为庆典式的狂欢、成为国家民族摆脱癌瘤、重获新生的节日欢庆。 <br />
　二十年前，同样面对数以百万计的游行示威民众，中共的顽固派们却惊人的沉着、冷静、精明、团结，他们周密部署，统一指挥，一防兵变、二防政变，双管齐下，以数十万野战军、数百辆坦克、装甲车的绝对杀人优势，将中国的八九民运屠灭于血泊当中。 <br />
　面对如此惨酷血腥的屠杀同胞场面，中共的将领和高官的表现却惊人的冷血，没有一个将领敢于倒戈相向、没有一个军委军头敢于与邓小平对着干；而中共开明派的表现却又惊人的懦弱，由儒家敬老、孝道观念而生的中国老人政治传统，深深地毒害了赵紫阳等人，使其宁可弃职逃避，也不敢抓住时机推翻邓小平、陈云等老东西。 <br />
　缺乏人道主义的社会传统＋老人政治传统＝中国八九民运失败。中国八九民运失败的悲剧民运，实际上在胡耀邦促成邓小平、陈云、王震、薄一波等老干部，形成老人干政集团的那一天，就注定了。 <br />
　中国儒家传统社会人道主义土壤的稀缺，使类东欧“天鹅绒”式的和平革命在中国成功的希望非常渺茫，六年来更加阴狠毒辣的暴政表明，象今上这样的中共统治者，只要还有一口气，必要时也会毫不犹豫地选择开枪屠杀和平反抗的民众，1989年、2008年，两次“西藏镇暴”，就是典型的例子！ <br />
　而人道主义土壤的稀缺，也是中国宪政民主转型的一大障碍；因此，培育中国的人道主义新文化，也应该是中国自由文化运动的目标之一。 <br />
　　　　　　　　曾节明　二九年三月十四日星期六中午</div>

							
							
							
							
