“南方报系既然发动这个论争，就应该发表正反两方的观点”<br />
<br />
　　早报：自从3月份“汪晖涉嫌抄袭”发生以来，除了接受媒体采访即兴的一些发言，学者认真论辩的并不太多，主要是媒体在参与这样一次讨论，你如何看学术界的这样一种沉默？ <br />
<br />
　　李陀：在这次论辩里，实际上是大众媒体（主要是南方报系）把一个学术性很强的论争包揽过来，几个月来发表了很多相关的文字。但问题是，媒体这么操作是有缺点的，有些学者可能不愿意介入媒体的讨论――今天媒体的影响太大了，如何处理学术和媒体的关系，不是每个人都有经验和能力。另外，中国大学的腐败已经是公认的事实，很多媒体都提出了尖锐的批评，这是一个少见的舆论环境，学者如何介入媒体，不能不是一个难题。另外，南方报系的做法也有问题，他们既然发动这个论争，就应该发表正反两方的观点，有充分的争论，可事实并不是如此，造成的讨论气氛比较怪，没有形成真正的论争气氛。<br />
<br />
　　早报：在你看来，媒体参与到这场讨论中来，它的位置应该是什么？因为听到不同的声音，认为媒体在这场讨论中立场并不客观。<br />
<br />
　　李陀：媒体在这场学术性的论争中保持客观很难，因为这场论争，涉及许多技术性和学术性的问题，许多专业概念、写作规范应该如何理解其实是争论的关键，而这恰恰需要在这次论争里弄清楚。问题是，这主要是学术界才能解决的事情，媒体起到的作用是检举、监督，同时适当地提供平台进行论辩。<br />
<br />
　　早报：公开或者非公开的言论中，其实已经认定汪晖是“抄袭”，你怎么看这些观点？ <br />
<br />
　　李陀：我个人的观点也认为，汪晖的文章有缺点，有不规范和不严谨的问题。但我还认为，个人看法不能算数，要确认汪晖是否抄袭，应把那些被质疑的句子、论点、段落，一个个都拿出来，认真分析，逐条论辩。现在不是“表态”的时候，结论应该放在事实清楚之后。让我所担心的是，这种辩论应该有个过程，有来有往，形成双方若干回合的辩驳，现在参与的双方是不是有这样的耐心？其实，中国思想史和学术史上，也有过类似的公案，也有类似的辩论，不过双方都是来回往返地辩论，不能一锤定音。<br />
<br />
“林毓生参与讨论后，学术问题背后的政治问题浮现了出来” <br />
　　早报：不久前，著名学者林毓生接受媒体采访，对“汪晖事件”发表了评论，使原本已经平静下来的“汪晖事件”再次热起来，林先生等学者的介入对“汪晖事件”的发展，有何积极意义？<br />
<br />
　　李陀：林毓生先生这次站出来介入讨论，本来是个很好的机会。他有在美国多年执教的经验，对西方的学术规范也应该有更多的理解，对类似问题如何解决，应该有很好的经验。因此，本来他可以起到一个模范作用，以一个学者的平等身份介入这次论争，做出有说服力的分析，和大家一起把事情弄清楚，是就是，非就非。<br />
<br />
　　早报：但有学者认为，“汪晖事件”论争背后是政治问题，立场问题，而林先生的介入使本来就存在的路线之争明朗化，你是否同意？<br />
<br />
　　李陀：林毓生先生介入之前，对于汪晖涉嫌抄袭的一些争论，大致还都限于学术和学风的层面，但他的介入，一下子扩大了讨论的边界，把本来和“抄袭”无关的一些内容带了进来。所以“汪晖事件”在林先生参与讨论之后，其实是被政治化和复杂化了，学术问题背后的政治问题浮现了出来。<br />
<br />
　　<strong><font color="red">早报：那你是否认为，现在新左和新自由主义派索性应该进行公开的论争，公开讨论思想论争背后的政治含义？<br />
<br />
　　李陀：首先，我愿意再声明一次，我始终认为用“新左”和“新自由主义”这样的说法来形容、概括中国知识界和思想界的分歧，是不准确的，因为实际情况要复杂得多。不管林毓生先生主观意图是什么，他的谈话又一次把这些复杂的分歧凸现了出来，那么，索性把一直没有充分表现出来的论争借此公开和展开，也不错。学术、思想本来就应该有各种声音，思想的大一统才是最需要警惕的。1990年代，思想界曾经就所谓的新左派和新自由主义的思想分歧，发生过一场公开争论，我一直认为，那是一次很宝贵的机会，如果那一次能够充分展开，中国的知识地图肯定和今天大不一样。但是，由于种种原因，那次争论没有持续，很可惜。林毓生先生这次的介入是旧话重提，重新把以往的一些思想和理论冲突提上议程，但是，今天的中国和世界都发生了重大的变化，实际的冲突和思想的对立都已经具有新的历史内容，因此，如果能在理论和思想界发动一次有很多人参与的更广泛大讨论，大争论，那是再好不过了，希望这一次不会半途而废。</strong>[/color=red]<br />
