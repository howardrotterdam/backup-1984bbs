“汪晖事件”升级&nbsp;&nbsp;李陀发《给林毓生先生的一封公开信》建议其撰文将论争表面化<br />
<br />
　　“汪晖涉嫌抄袭事件”已经过去两个多月，有一段时间里媒体和学者对此事件关注度开始下降，原本以为此案又会不了了之，但随着著名华人学者林毓生先生通过接受内地媒体的采访介入“汪晖涉嫌抄袭”讨论，“汪晖事件”再次被媒体和学术界强烈关注。昨天，学者李陀向早报记者独家发来完整版《给林毓生先生的一封公开信》，并接受早报记者专访，他就“是否涉嫌抄袭”、如何就此事进行论辩以及广泛的思想学术论争，发表了个人观点。<br />
<br />
　　早报记者 石剑峰<br />
