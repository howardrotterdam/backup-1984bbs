林毓生 赵静 早报资料<br />
<br />
　　李陀建议林毓生<br />
<br />
　　分析辩驳“争论”<br />
<br />
　　6月10日起，在网络上开始流传一封李陀致林毓生先生的信。昨天，经早报记者向李陀证实，该信确为其所写，但网上流传的只是一个初稿，也不知怎么流传出去，而给早报记者的这封公开信为最终定稿。<br />
<br />
　　李陀在信中首先认为，自王彬彬的文章《汪晖〈反抗绝望――鲁迅及其文学世界〉的学风问题》在《南方周末》重刊以来，“关于汪晖究竟是否涉嫌抄袭之事，国内学界是有争论的。王文发表之后，钱理群、孙郁、赵京华等学者都发表过看法，但是，由于他们大多是在被媒体采访的情况下，就事论事发表了一些不同意见，我以为可以暂且置之不论。问题是，此后，还有几篇很认真写就的与王文争辩的文字，如钟彪的《驳王彬彬的诬蔑：学术“私律”与莫须有》、舒炜的《“王彬彬式的搅拌”对学术的危害》、魏行的《媒体暴力与学术独立――关于一起媒体公共事件的备忘录》，这些文章与网络上的许多所谓‘倒汪’和‘挺汪’的意见和言论有所不同，是严谨的，是对相关材料作了认真研究的，是针对王文（以及其他一些人的文章）有的放矢，提出了不同的具体材料和论据，逐条与王彬彬等人商榷的。”<br />
<br />
　　但在李陀看来，这些与王争辩的文章出现之后，并没有造成一个相对说理的辩论局面，其中的一个重要原因是这场针对汪晖“涉嫌抄袭”的论争开始于大众媒体，“虽然始自《文艺研究》，但发动者和推动者实际上都不是学术刊物，而是大众媒体，对于开展一场具有相当学术性的辩论（诸如对《反抗绝望》一书各版本之间注释异同的繁琐比较），这样的平台显然有其局限性。为此，如何在学术和舆论之间做好沟通和平衡，已经成为当前如何究竟是非的一个十分关键的难点。”<br />
<br />
　　李陀在信中说，原本以为林毓生的介入，此次论争可能会形成好的转机，“不过，仔细读过你的相关谈话，以及这些谈话中的意见和结论之后，坦白说，我相当失望。”李陀给出的“失望”理由是，“不论是有意还是无意，你在《南方都市报》和《新京报》上的言论完全没有提及对汪晖涉嫌‘抄袭’还有不同意见，还有辩论，白纸黑字，还有钟、舒、魏诸人的文章，正是这一点使我产生很大的疑惑。我想，虽然你在谈话里最核心的一点意见，是提出清华大学应该组织调查委员会（如果清华大学不这样做，校长就应该下台），但读过你谈话的人，任谁都明白，其实你已经做出汪晖是抄袭者，甚至是个‘抢夺’者的结论。这当然是一个很严重的结论。实际上，经过这几天各种纸媒和网络媒体的散播，你的说法已经对一位目前只是‘涉嫌’的学者形成极大的伤害，甚至可能影响他的终身。我还想，以你多年在美国大学执教的经历，应该明白一个学者对自己的一位同行作出这样的指控，都负有什么法律和道义的责任。”李陀建议林毓生是否可以针对钟、舒、魏诸人的文章做一次认真的分析和辩驳，“写一篇论辩文字可能要花费你一些时间，但是，不仅别的人，就是你自己，也应该担起相应的政治与法律的责任，写这样一篇文章不仅是值得的，也是必要的。”<br />
<br />
　　“涉嫌抄袭”<br />
<br />
　　辩论边界扩大了？<br />
<br />
　　在这封公开信中，李陀还认为林毓生最近接受媒体采访的言论，“把关于汪晖是否涉嫌‘抄袭’问题的辩论边界扩大了，即把汪晖的思想和学术的政治内涵、政治倾向也放了进来。”李陀提到，王彬彬在《南方周末》上发表文章的3月25日当天，美国费城也正在举行亚洲协会年会，来自加拿大的邱慧芬教授作了一个题为《基本人权和西方民主》的对汪晖的学术研究和理论立场进行全面批评的发言，该发言日前发表在香港的杂志上，并特别申明“最初的英文发言稿，曾由我的启蒙老师林毓生先生过目，并给予宝贵意见，谨此致谢”。<br />
<br />
　　“由此我不能不猜想，你对汪晖问题的关切，不只限于‘抄袭’，而是有更大的想法，关系到当今中国和世界读书人都在思考和争论的很多大问题。如果我这猜想不差的话，我希望你也就此写出文章，更系统地在学理层面做出详细的阐述，把隐约中的论争表面化，尖锐化。如果有这样的文章开局，我相信定会形成一场意义重大的论争，还相信这论争绝不会仅限于你和汪晖之间，一定会有更多的人参与其中，大狗叫，小狗也叫，百家争鸣，这是何等令人向往的局面？先生何乐而不为？”<br />
