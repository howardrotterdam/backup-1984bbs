																											<div id="postmessage_236506" class="t_msgfont">古代为什么没有标点符号？因为他不需要读那么快。标点符号的流行只是近两三百年间的事情。标点符号是怎么诞生的呢？它其实是一个商业行为的结果。五六百年前，欧洲出现了古登堡印刷术，书籍出现并成为最古老的工业产品，进入市场成了商品。出于成本考虑，印刷商也就是书商就想做大量印刷来摊薄成本，于是他们发明了标点符号，让书变得更容易读、读得更快。这还不够，还要分段。<br />
<br />
古人的书是没有分段的，也没有章节。今天我们看《论语》，一篇与另一篇之间是没有关系的，卷只是物质上的单位，竹简不够了，再换一卷继续写。中国古代的书是这样，印度、阿拉伯、欧洲的书也是这样。印刷术出来之后，出版商才开始给书籍做段落篇幅的划分。<br />
<br />
分段产生了很大的影响。以前欧洲人读不分段的《圣经》每次都老老实实从开篇读起，《圣经》分段以后，人们开始认为不同的段落重要程度不同，分歧就产生了。所以英国大哲学家约翰&#8226;洛克说：“《圣经》分段印刷是无耻商人酿制的悲剧，他们为了让《圣经》更畅销强行分段，分段以后我们的信仰将四分五裂。”后来的结果被洛克不幸言中――基督教分裂成一大堆教派、教会。所以怎么印实在很重要，印刷术的产生令我们离开精读，开始泛读。<br />
<br />
很多人问我，如何读得快一点，但其实如何读得好更重要。古人是怎样读书的呢？他们不是看，是反复地读，慢慢地读，读出声音来。朗读能让人沉浸到书里面去，这样一个沉浸的状态会产生一个重要的效果――这就是修炼的效果。<br />
<br />
我手中是一本《沉思录》，这本书现在在中国很热，因为Wen Jiabao总理在看，克林顿、布什、密特朗都看过。奥勒留并不是为出版而写的，他是写给自己读的――不断研读，修身养性。这本书最有趣的地方就在于它是一本修行集，他的写作本身就是修行，之后的不断研读也是。写作，在英文里面就叫Spiritual Writing，所以它的读者也要把它当作一个精神修炼来读，不断地读、反复地读。这些领导人一遍遍地读《沉思录》，就是在做精神修炼。古人读《论语》也是这样。所以读书是为了改变自己，变化人格，变成另外一个人。这一点古人都是知道的。<br />
<br />
很多人一辈子读《红楼梦》，每次都看到不一样的意思，古人反复读为的不是这个，他是每次都读到同一个意思。他是要不断地再三地提醒自己，比如，“当一个人对你做错事情之后，你不要愤怒，你反而要去想他为善或为恶的理由，然后你就不会愤怒了”，容不容易懂？做不做得到？这种书不是要你明白它的意思，是要你去做。所以真正读书读得好的人都会被某一本书变成另外一个人。古代读书人一定要是一个好人，如果不是一个好人证明书没有读好。所以读书是一种修炼，一种transformation(转变)。<br />
<br />
我为什么讲这个？因为我发现今天大家只是把读书当做一个工具，通过它拿到什么东西。读书其实是一种精神操练，是我们已经失落的一个传统，我们每个人都可以恢复它，找一本书去修炼自己，追求自己的变化。<br />
