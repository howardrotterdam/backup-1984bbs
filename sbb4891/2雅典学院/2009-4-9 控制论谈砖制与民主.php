																											<div id="postmessage_71155" class="t_msgfont">控制论谈砖制与民主<br />
维纳创建的控制论已成为现代科学一门重要的学科；在现代工业得到了广泛的应用。<br />
应用控制论的产品也走进我们日常生活。如冰箱，空调就是控制论应用最好的实例。<br />
控制论通俗讲就是把一个系统控制在某一个稳定状态下。<br />
冰箱，与空调共同的特征具有恒温的功能。<br />
用控制论实现恒温最经典的方法就是“负反馈”系统。<br />
以空调为例，把室内要控制的温度，作为系统控制的设定值，即遥控上的设定温度值；<br />
用一个热电阻来测量室内实际温度；将实测温度与设定温度对比，如没有偏差，则维持原状；如正偏差，则停止致冷；负偏差加大致冷量。<br />
这就是一个负反馈控制系统；一个控制系统建立后，运行还须考核控制系统品质因素。空调控制系统品质因素就是被控参数的温度的稳压定性；超调幅度，调节周期。<br />
<br />
有了上述基本概念，就可以对社会两种不同制度下社会稳定控制过程进行分柝了。<br />
回顾中国社会数千年王朝兴亡，盛衰的历史。也可用控制论找出其规律。<br />
社会安定国富民泰，又称太平盛世。可作为社会系统的一个目标值。<br />
<br />
当一个王朝的建立与兴起，是往往比较注意民间疾苦；但王朝是以血缘来传承，在宫庭中成长的皇上，对民间缺乏了解，官员腐败得不到控制，当民众承受的经济负担，突破血酬定律限值，官民对抗就上升暴力对抗。农民起义成了社会重新找回社会的平衡点。这就是砖制社会的控制系统。</div>

							
							
							
							
