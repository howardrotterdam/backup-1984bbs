																											<div id="postmessage_71158" class="t_msgfont">选票制衡原理&quot;<br />
选举制度是现民主政体体制运行重要的一环。<br />
<br />
选举制度是区分强权政府与服务型政府的分水岭。<br />
<br />
<br />
现世界各国政府可分两类即“强权政府与服务型政府”<br />
<br />
“强权政府与服务型政府”的官员在社会中的也扮演了不同的角色。<br />
<br />
强权政府的官员是全社会秩序的管理者；服务型政府官员则是社会的服务者。<br />
<br />
社会是由各阶层组成，雇主或又称老板，雇员或称打工仔；这两部分人构成了社会主体。雇主与雇员分属社会不同的群体；按我国通常说法：雇员是弱势群体。<br />
<br />
在强权社会中，雇员是赤贫的弱势群体，靠政府权力者的恩赐赖以生存。雇主则可用金钱来换取官员的支持，雇员与雇主地位是不相等的，雇主与雇员两者地位不等，必然导至两者雇佣合同关系不平等与不公正。在官员，雇主，雇员三者中，雇员被任意鱼肉的弱者。<br />
<br />
在服务型政府，政府权力来自定期举行的选举。由全社会公民选举产生政府官员。雇员这群体是在全社会人数中拙对多数。任何真正的民主选举中，参与竞选的政客均不会忽视“打工仔”的声音。因此，这些“打工仔”成了选举主要票源。政府官员为了谋取下届连任，执政时，所作所为也不得不考虑“打工仔”们的利益。打工仔从赤贫的弱势群体演变成政客们参政不得忽略的强势票源。老板与政府官员的关系也有质的变化，官员不再是凌驾在社会上的太岁。雇主与雇员成了较为单纯的两元关系。政府仅起了裁判作用。若雇主与雇员两者处于平等地位，雇佣关系才可能是公平的“双赢”。社会才会有公正。</div>

							
							
							
							
