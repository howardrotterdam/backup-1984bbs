													<h2>有人请薄熙来当下一任总书记</h2>
						
						
																											<div id="postmessage_284251" class="t_msgfont">薄熙来应该当下一任总书记的六大理由<br />
2009年10月16日 转载&nbsp; &nbsp; 来源：解滨博客<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;既然这么多年后又要喊毛泽东思想万岁了，那我们就不能光说不练。毛主席教导我们：“政治路线确定之后，干部就是决定的因素”。 胡温新政届期将满，现在又到了指定下一代党和国家的领导人的时候了。 虽有“人民创造历史”一说，但我国情况特殊，只有英雄才有资格创造历史，人民是提不起来的阿斗，只能被利用。所以我们不能搞西方国家的民主、人权、法治那一套，更不能接受他们的普世价值。 我们要坚持老祖宗的家法，走有中国特色的社会主义道路。 毛一代废了刘少奇、林彪后立了华国锋为接班人。邓二代废了胡耀邦、赵紫阳后立了江泽民。 现在，到了胡四代该废谁立谁的时刻了。 至于谁该被废掉，还用我说吗？ 习近平同志说得客气点是忠厚老实，说得刻薄点是平庸。老实也好，平庸也好，都不能当饭吃，更不能成为当官的资本，何况这是做一国之君。 有人不想当亡国之君，难道谁想当亡国之民吗？ 既然如此，废了他又该立谁呢？ 当然是薄熙来。<br />
<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 理由之一，和习大公子一样，薄大公子也是“正红旗”，太子党，嫡系，根正苗红。 用邓妈妈的话来说：“还是咱们自己的孩子信得过”。如今这世道，人心不古，世风日下，人们口是心非，当面阿谀奉承，背后捅刀子。 除了自己的孩子，谁都信不过。 大凡高度稳定的国家，多实行世袭制，权力传内不传外，如朝鲜，沙特等。彭德怀当年没有护好皇太子，造成了党和国家领导人接班困难，成千古罪人。 我们要维稳，怎么着也要自己家人接班。 皇亲国戚中出类拔萃之辈虽不多，却有一人仪表堂堂， 出口成章，才华超群，生就帝王相。这就是薄公子。 薄公子登基，祖上打下的千秋大业，在自己人手中紧紧攥着，党放心。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 理由之二，和习公子不一样，薄熙来是毛后、邓后、江后、胡后的一颗耀眼的新星。几千年来，百姓就一直在黑暗中苦盼明君的到来，终于盼来了人民的大救星毛泽东。 虽然大救星在万岁、万岁、万万岁的欢呼中还是远离我们去了，我们后来有了邓总设计师规划蓝图，江总书记继往开来，胡总书记科学发展，所以才有一甲子的辉煌。胡温后，问苍茫大地，谁主沉浮？ 毋庸置疑，当然是当今中国之大英雄薄熙来。 有人说薄熙来乃中国的普金，有人说他是大陆的马英九。 普京也好，马英九也好，反正他的本事在那里，谁也遮挡不住他的光辉。薄熙来不愧为中国当今政坛最璀璨的明星！ <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 理由之三：和高高在上的习公子不一样，薄熙来还是人民的小救星。当年薄熙来当上大连市长后，敢闯敢干，轰轰烈烈，砸锅卖铁造花园，尽情挥洒，把个大连市搞的花团锦簇，天翻地覆；提辽宁省长后，另辟蹊径，反腐开道，强龙专压地头蛇，挖出“慕马大案”，给沉闷的东北官场吹进了肃贪求变的春风；转任商务部长后，洋务救国，交涉谈判，妙语连珠，游刃有余，终将中欧纺织品摩擦化于无形；调动重庆后，薄熙来打黑除恶，整顿社会治安，铁腕治蜀，震动全国。短短一年，薄熙来挥洒自如，取得傲人成绩，再一次展示了他过人胆识和举重若轻的才能。二十年间，薄熙来以其辉煌，人口皆碑的业绩，擅解难题，举重若轻，敢闯务实。当年百信中流传的&quot;要吃粮，找紫阳，要吃米，找万里&quot;也不过如此。 在当今我党官场上，难觅可媲美薄熙来的第二人。 有人云，中国有50个薄熙来，中国的问题就解决了。他薄熙来只要当上总书记，威力何止100个今日的薄熙来？<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 理由之四，薄熙来是当代铁面无情的包公。在中国，制度不吃香，包公永远吃香。 靠制度反腐永远是个不可实现的奢望。 因为制度反腐的最大一个问题就是邓公所担心的 “反腐败亡党，不反腐败亡国”之矛盾。 试问党国之中，是党大，还是国大？自然是党大。 60大庆说了，没有党，就没有国。既然如此，国可亡，党不可亡，这是原则。 我们可以让960万平方公里土地统统被污染掉，让8亿农民永远穷苦，让13亿百姓永远为美帝国主义打工，但不能一天没有党。 眼下能保住党的， 唯有薄熙来。因为党已经腐败透顶，薄熙来是制度内将反腐推进到最远的英雄。 别人只敢装模作样地小打小闹， 唯有他敢大打大闹，白刀子进，红刀子出。他就是当代包公。薄熙来当总书记，党国至少要延寿10年。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 理由之五，薄熙来大智大勇。 胡温之后的中国，政局必是诡异多端。 如今在京城搞一个60庆典，要国家一半的陆军来弹压。 小小一个乌鲁木齐出现骚乱，要调动半个国家的武警去镇暴。胡温之后，江总书记年事已高，估计剩下的日子不多了。多事之秋，不稳定的因素逐年增多，群体事件稳步上升。 要是出现一个大骚乱的局面，几千万百姓暴动，几十个城市烧大火，几百个国企老总被愤怒的工人踩死，四平八稳的老实人习近平同志搞得定那种局面吗？邓总设计师开枪镇压的事，已经写进了史书，后人要骂一万年的，所以不敢再做了。 那谁能既不开枪又能搞定局面呢？ 迄今为止，共产党几百万官员中敢于只身跑到大街上和罢工群众打擂台，当场拍板拿定解决方案，不但没被打死反而得到罢工者首肯的，只有薄熙来一人。 <br />
&nbsp; &nbsp; <br />
