<br />
　　首先我们把矛盾双方确认，一个学生与他的学校，至于什么经纪公司和教育部等我们都暂不考虑。现在发出声音的仅仅是学生这一边，而两天过去了，学校方面没有做出正式的表示，虽然他们有官方和官方的媒体渠道……当然，可能是出于不屑与傲慢，一个女生或者“害群之马”还惊动不了他们这么大的庞然大物？不过似乎已经惊动了，一些比较像样子的帖子应该是运作出来的，或者说沉默乃至装出来的不屑对上位者来说也是一种风度？ <br />
<br />
　　OK，当然，我没有忽略《南方都市报》透露出的校方信息，这样一个少有的、我所尊重的媒体的讯息我又怎么能不重视呢？但同样的，南都的这篇报道也令我失望，虽然报道中有许多信息是从学校来的，但是全都是“北外校方”、“自称北外学生的网友”，连个姓氏也没有透露出来，至少香奈儿的博客上还有个“北外刘老师”吧。说这个“香奈儿”早就因为签了经纪公司申请退学，那么退学申请的照片能惠赐一张吗？No！没有。难道是《南都》的版面太珍贵？No！香奈儿的浓妆艺术照倒是有一大张，在这个保守的国度，这样的照片确实能在不触动任何事实的前提下给公众带来一些恶感。总之，一屁股坐到校方那边去了。(南方都市报报道：<a href="http://epaper.nddaily.com/A/html/2009-03/17/content_731961.htm" target="_blank">http://epaper.nddaily.com/A/html/2009-03/17/content_731961.htm</a>) <br />
<br />
　　在我们这个国度，我们喜欢驯良的小孩，纯情的女生，我们总会把自己在某一方面看不过眼的人归于异类，“它”们所遇到的一切与“我”无关。乃至怀有恶意，影响到自己的是非观，判断力。无论“它”们与我们多么相异、多么不同，无论如何，我们生活在一个世界、一个制度、一个体系，“它”们的遭遇我们和我们的后代血亲都可能身受。请不要忘记德国神父的哀叹：他们杀共产党人，我没有说话，因为我不是共产党人；他们又杀犹太人，我没有说话，因为我不是犹太人:他们杀工会成员，我没有说话，因为我不是工会成员；他们杀天主教徒，我没有说话， 因为我是新教教徒；最后他们杀我了，却再也没有人能够为我说话了，能够为我说话的人都被他们杀光了! <br />
<br />
