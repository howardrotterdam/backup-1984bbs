													<h2>网络评论员：微博是一种倒退 而非革命</h2>
						
						
																											<div id="postmessage_600001" class="t_msgfont">4月30日《环球时报》特约时评<br />
微博已经大行其道了。“微博蕴含巨大的商业价值”、“微博将成基本的沟通方式”，对微博的歌功颂德之声不绝于耳。不仅微博，如今的媒体热衷于一边倒地歌颂 互联网，歌颂互联网上的一切创新;就象打了鸡血一样的兴奋。事实上任何事物都有两面性，互联网也不例外，创新也不例外。我去年写过一篇文章，叫《创新的边 疆》，就是说创新也是有边界的，越了界就不是什么好事情。<br />
<br />
在当前的互联网创新中，博客是很好的创新。它释放了民众捕捉问题、分析问题的兴趣和热情，每个有价值的见解都在适当的篇幅下被积极地诠释和传播。然而写“这么长”的文章总是不太让有些人“过瘾”，于是乎博客被异化成“必须”低于140字的短、平、快信息，成为“微博”，并在互联网界、营销界被广泛追捧。微博确实是一种创新，但在我看来，它却是一个负向的创新；不是一场革命，而是一场倒退。很有讽刺意味的是，有人在网上呼吁：“请各位给一个能让我开通某网微博的邀请码。如果不给，女友要和我分手!”这就是微博时代浅薄的爱情和轻薄的思维。2012是否真的世界末日也许已经无所谓了，以微博这个趋势，波兹曼预言的《娱乐致死》大概2012年之前就已经到来了。<br />
<br />
很多人认为微博大大加快了信息的速度和效率，所以受到追捧。如果是为了信息传递的快捷，那么即时通信工具(比如QQ、MSN)就很好，而且比微博更快，何需微博?“围观”这个流行的微博词汇，准确地道出了微博窜红的原因。围观其实就是看热闹，人因为好奇心的缘故都喜欢看热闹。业内有说法是“微博的核心就在于转发，不停地转发”，互联网无疆界无距离，既然微博促成了基于互联网的大规模“围观”，那么各种小道消息、甚至造谣诽谤都能以最快的速度传播到四面八方，满足大众的猎奇心理。新闻需要什么？快；新闻怕什么？短！短，就意味着缺乏足够的分析和推理，意味着误导。记者不是思想家，如果做了引导的事情但不承担引导的责任，结果就是误导;所以记者作为新闻工作者，一定要经过专业学习和训练。而微博让每个人都变成记者，而且是未经训练的记者，制造出大量不加分析的结论、断章取义的理解和缺斤少两的信息。没有无限的快捷，微博造就的不是信息交流便捷，而是成为了垃圾信息甚至有毒信息的工厂。信息是思考的素材；信息是蔬菜、肉、蛋、禽，思考则是烹饪，未经烹饪的东西一古脑让人吃下去，早晚得病！<br />
<br />
有时候人们需要时间让自己暂时成为一个信息孤岛，就是“个人空间”和“个人隐私”。以Twitter为代表的微博让这种愿望彻底化为泡影;你的信息不见得只有你自己才会发布，或者经过你的同意才会被发布。你身边的每一个人，不管是陌生人还是熟人、朋友，都可能是一个Tweeter(推客，发布微博的人)，不经你的同意就将你的健康状况、你的一举一动发布出去，在你不经意的时候让你成为众矢之的。换句话讲，人人都是狗仔队。即使是你主动发布的，也许你也会因为一句口无遮拦的话一夜间引来万人唾骂，而你却没有解释的机会和道歉的余地。自由不应该是胡闹，更不是不假思索的起哄。中国人常说“口水也能淹死人”，微博则更加放大了口水的作用和速度，是一汪具有蝴蝶效应的口水之“啸”。<br />
<br />
讲个故事。两年多以前的某个晚上，我家楼下的十字路口(二环路上的一处斑马线)曾经发生一起恶性交通事故。晚10点左右，正在写作的我听见凄厉的刹车声和碰撞声，紧跟着又是较小的碰撞声。冲到阳台上之后，我看到路边停着公交车，公交车旁边趴着重伤的行人。我第一个报了120，描述是：“某某路口，公交车撞飞行人，重伤在地不能动弹，请速派救护车”。3分钟之后，伤者动弹了一下，试图撑起身子没有成功；5分钟之后，救护车赶到。我为自己作为一个“热心市民”可能挽救了一个生命而感到欣慰。不过今天讲这个故事的重点不在这里。我是在设想，如果那时候有微博，并且我拥有不少的微博“听众”(即“围观”者)的话，那么可能我就会从楼上拍下照片上传到微博，顺带将我报120的话原样发布出去(没有超过140字，简短而“准确”)。可我的一时口舌之快会产生什么样的影响呢？消息很可能在很短的时间内被反复转载、扩散，随后会有各种声音开始声讨公交车司机，甚至针对某市公交。然而警方经过认真调查和现场勘察之后，事实却是一辆黑色轿车撞飞路人并迅速从岔路逃逸(后自首)，伤者先跌落公交车顶，然后再滚落地面，形成我看到的“第一现场”。因与公交车间接相关，司机和乘客停下积极报警并配合事故勘察。<br />
<br />
如果广播诽谤你，你可以控告电台;如果电视诽谤你，你可以起诉电视台。但如果某人因受微博诽谤而声名受损，你甚至不知道你该起诉谁。因为发布者可能就在你身边但却匿名，而传播者却是千千万万毫不知情的好事者，而微博网站也因为对内容“不知情”、“不可控”而免责。<br />
<br />
“没有调查就没有发言权”，法庭为什么不只是嫌疑人与法官的对话，而一定要有律师，甚至陪审团?就是为了发现真相、辨明真相;不放过坏人、但也不冤枉好人。真相往往不会浮在表面上。微博却反起道而行之，将一切表面现象当作真相，用最快的速度传播出去，几乎要让真相永远没有浮出水面的机会。喜欢营销的商人当然喜欢微博，因为营销往往需要掩盖真相;喜欢打造广告平台的人也喜欢微博，因为他们又看到一个大号的“聚宝盆”。但这些，却是以牺牲信息有效性为代价的，其最终效果是大众的去智化。<br />
<br />
虽然“群众的眼睛是雪亮的”，但愤怒却能蒙蔽民众的眼睛。大哲学家苏格拉底被愤怒的古希腊公民判处死刑，罗马女天文学家希帕提娅被愤怒的基督徒推进死亡;事实上他们比同时代的任何人都更接近真理。今天和未来的微博，恰恰就是最能点燃愤怒的火种。迪吧、俱乐部、夜总会，为什么这些地方能让人那么狂热?因为在那里人不需要思考，只需要一起扭动、一起叫喊，就能够“HIGH”。而微博，就在一秒之内辐射全球的互联网上，给人们创造了可以与万里之外的陌生人一起看热闹、一道起哄，和不负任何责任不承担任何道德风险地实现瞬时传播的地方。这是一件多“HIGH”多“酷”的事情啊!<br />
