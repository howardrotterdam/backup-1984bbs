抵 达 北 京 後 ， 陈 清 华 才 意 识 到 那 是 暴 风 雨 前 的 平 静 。 当 时 ， 北 京 高 校 学 生 自 治 联 合 会 （ 北 高 联 ） 的 学 生 领 袖 已 估 计 ， 政 府 正 盘 算 下 一 步 行 动 ， 军 队 开 始 入 城 ， 「 但 成 坛 S 其 实 好 自 发 ，  人 完 全 话 到 事 ， 同 学 仔 要 留 低  就 留 低 。 」（「 但 整 件 事 情 其 实 很 自 发 ， 没 有 人 能　完 全 主 导 ， 同 学 们 要 留 下 的 就 留 下 。 」）<br />
