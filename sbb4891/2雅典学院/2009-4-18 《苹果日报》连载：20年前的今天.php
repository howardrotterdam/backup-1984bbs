89 年 的 今 天 ， 香 港 专 上 学 生 联 会 （ 学 联 ） 首 批 代 表 上 京 ， 了 解 学 潮 的 发 展 ， 是 港 人 支 援 八 九 学 运 踏 出 的 第 一 步 。 陈 清 华 当 时 是 浸 会 学 院 社 工 系 的 学 生 ， 也 是 学 联 第 三 批 代 表 ， 後 来 带 同 筹 得 的 百 多 万 港 元 和 一 批 物 资 ， 上 京 支 援 学 运 。 <br />
