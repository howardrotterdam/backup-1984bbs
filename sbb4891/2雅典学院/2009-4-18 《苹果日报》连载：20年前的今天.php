<br />
<strong>离 京 前 一 直 被 跟 踪</strong><br />
逃 离 北 京 之 前 ， 陈 清 华 一 直 被 人 跟 踪 ， 「 当 年  便 衣 较 易 认 ， 戴 黑 超 ， 西 装 唔  身 。 」 （「 当 年 的 便 衣 较 易 辨 认 ， 戴 太 阳 眼 镜&nbsp;&nbsp;， 西 装 不 合 身 。 」） 最 黑 色 幽 默 是 事 後 才 发 现 ， 离 京 前 一 夜 他 们 入 住 的 饭 店 ， 顶 楼 就 是 国 安 局 的 截 听 总 部 。 <br />
<br />
20 年 过 去 ， 今 年 44 岁 任 职 社 工 的 陈 清 华 ， 早 已 由 热 血 大 学 生 ， 变 成 两 个 女 儿 的 父 亲 。 每 年 的 6 月 4 日 ， 陈 清 华 都 会 到 维 园 。 他 向 3 岁 的 孩 子 ， 这 样 解 释 那 点 点 烛 光 的 意 义 ， 「 我 同  讲 ，  学 生 好 好 ，  官 员 贪 钱 ，  O 去 请 愿 ， 但  官 员 曳 曳 ， 开 枪 打 死  O ， 死  就  得 翻 生 h 。 我 O  ， 系 为 纪 念  O 。 」 （「 我 和 她 说 ， 那 些 学 生 好 好 ， 那 些 官 员 贪 钱 ， 他 们 去 请 愿 ， 但 那 些 官 员 坏 坏， 开 枪 打 死 他 们 ， 死 了 就 不 能 再 活 过 来 了&nbsp;&nbsp;。 我 们 来 ， 是 为 了 纪 念 他 们 。 」 ）<br />
<br />
图 片 来 源 ： 陈 清 华<br />
<br />
<strong>一 生 承 诺 「 我 会 继 续 讲 真 相 」 </strong><br />
 <br />
事 隔 20 年 ， 陈 清 华 仍 清 楚 记 得 ， 六 四 後 他 离 开 北 京 前 的 一 个 承 诺 。 「 无 论 广 场 丶 医 院 丶 机 场 ， 都 有 人 叫 我 记 得 将 见 到  S ， 要 返 去 同 人 讲 。 」 (「 无 论 广 场 丶 医 院 丶 机 场 ， 都 有 人 叫 我 记 得 将 见 到 的 事&nbsp;&nbsp;， 要 回 去 跟 人 讲 。 」) 上 机 前 他 接 受 海 关 检 查 ， 还 担 心 关 员 充 公 他 们 的 菲 林 ， 谁 知 对 方 竟 是 提 醒 他 们 ， 要 把 菲 林 收 好 一 点 。<br />
<br />
<strong>当 年 同 窗 早 已 不 谈 六 四</strong><br />
<br />
这 些 年 来 ， 陈 清 华 都 有 履 行 承 诺 ， 到 学 校 演 讲 ， 参 加 支 联 会 的 讲 座 ， 最 近 还 到 香 港 大 学 出 席 论 坛 ， 与 港 大 学 生 会 会 长 陈 一 谔 等 学 生 辩 论 。 他 说 ， 其 实 很 怕 重 提 当 时 细 节 ， 回 想 起 幕 幕 的 画 面 ， 「 但 为  死 去  同 伴 ， 呢 个 系 我  责 任 。 」&nbsp;&nbsp;（「但 为 了 死 去 的 同 伴 ， 这 个 是 我 的 责 任 。 」）他 说 ， 近 年 参 加 讲 座 ， 不 时 「 同 人 嗌 交 」（吵 架） ， 又 有 跟 左 派 有 联 系 的 朋 友 ， 叫 他 回 内 地 看 看 现 在 中 国 的 发 展 。 <br />
<br />
「 我  答 案 系 ， 我 知 道 ， 但 呢  同 （ 六 四 ）  件 事 无 关 。 」 （「 我 的答 案 是 ， 我 知 道 ， 但 这 些 和（ 六 四 ） 那 件 事 无 关 。 」)）他 说 ， 尽 管 中 共 官 方 对 六 四 的 定 调 已 由 「 反 革 命 动 乱 」 变 成 「 政 治 风 波 」 ， 民 建 联 创 党 主 席 曾 钰 成 近 期 也 说 北 京 镇 压 学 生 是 错 误 ， 但 他 至 今 仍 无 法 释 怀 ， 「 到 今 日 ， 仍 然  人 出  为 当 时  决 定 道 歉 ，  人 尝 试 补 偿 受 难 者  痛 楚 ， 成 件 事  了 结 过 。 」 （「 到 今 日 ， 仍 然 没 有 人 出 来 为 当 时 的 决 定 道 歉 ， 没 人 尝 试 补 偿 受 难 者 的 痛 楚 ， 整 件 事 未 曾 了 结&nbsp;&nbsp;。 」）<br />
<br />
当 年 上 京 支 援 学 运 的 香 港 同 学 中 ， 有 的 因 为 在 政 府 工 作 丶 有 的 因 为 在 内 地 做 生 意 等 各 种 原 因 ， 早 已 不 再 谈 论 六 四 ， 但 陈 清 华 说 ： <strong>「 我 会 继 续 讲 真 相 ， 等 有 足 够  人 知 道 件 事 。 你 可 以 选 择 缄 默 ， 选 择 唔 讲 真 心 话 ， 但 你 要 知 道 真 相 系 点 ， 最 恐 怖  系 ， 真 相 被 遗 忘 。 」 （「 我 会 继 续 讲 真 相 ， 等 有 足 够 的 人 知 道 这 件 事 。 你 可 以 选 择 缄 默 ， 选 择 不 说 真 心 话 ， 但 你 要 知 道 真 相 是 怎 样 的 ， 最 恐 怖 的 是 ， 真 相 被 遗 忘 。 」 ）</strong><br />
<br />
直 到 今 天 ， 他 的 传 呼 机 和 手 提 电 话 号 码 ， 都 有 8964 这 组 数 字 。 <br />
