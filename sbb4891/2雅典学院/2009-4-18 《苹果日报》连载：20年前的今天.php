有 经 常 观 看 天 安 门 升 旗 礼 的 北 京 市 民 发 现 ， 天 安 门 广 场 昨 日 傍 晚 6 时 许 降 国 旗 後 ， 即 开 始 全 面 清 场 封 闭 ， 较 往 日 大 大 提 早 。 通 往 广 场 的 前 门 地 铁 站 等 各 大 出 口 ， 军 装 及 便 衣 公 安 也 明 显 增 多 ， 并 不 时 对 往 来 的 市 民 进 行 检 查 。 有 消 息 指 ， 由 於 北 京 已 进 入 敏 感 期 ， 当 局 对 天 安 门 广 场 的 戒 备 开 始 加 强 ， 提 前 清 场 就 是 其 中 措 施 之 一 。 据 悉 ， 该 措 施 将 持 续 到 5 月 1 日 ， 之 後 才 恢 复 到 晚 上 10 时 清 场 ， 但 到 5 月 底 近 6 月 ， 天 安 门 广 场 每 日 将 再 度 提 前 清 场 。 <br />
