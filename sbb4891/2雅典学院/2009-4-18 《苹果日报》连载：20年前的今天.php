<br />
尹 敬 <br />
公 务 员 ， 生 於 一 九 五 三 年 三 月 廿 一 日 ， 故 於 一 九 八 九 年 六 月 四 日 。 <br />
吴 向 东 <br />
夜 校 生 ， 生 於 一 九 六 八 年 八 月 十 三 日 ， 故 於 一 九 八 九 年 六 月 四 日 。 <br />
孙 铁 <br />
银 行 职 员 ， 生 於 一 九 六 三 年 十 一 月 三 日 ， 卒 於 一 九 八 九 年 六 月 三 日 ， 墓 碑 刻 上 「 愿 英 灵 永 在 」 。 <br />
<br />
跪 在 他 们 坟 前 ， 是 香 港 教 师 萧 绮 熙 。 她 生 於 6 月 6 日 。 大 概 从 中 七 开 始 ， 她 没 再 提 前 庆 祝 生 辰 。 生 日 前 夕 ， 她 的 脸 上 ， 只 有 哀 伤 ， 只 会 穿 上 黑 色 衣 裳 ， 在 维 园 ， 化 作 一 点 烛 光 。 烛 光 很 暖 ， 却 遥 远 。 五 年 前 ， 她 闯 进 北 京 八 宝 山 人 民 公 墓 ， 在 三 名 六 四 死 难 者 坟 前 献 花 丶 默 祷 ， 将 香 港 人 多 年 来 遥 遥 默 默 的 哀 思 ， 由 铜 锣 湾 送 进 京 城 。&nbsp;&nbsp;记 者 ： 雷 子 乐 （注：维园位於铜锣湾）<br />
<br />
那 是 04 年 盛 夏 ， 当 年 23 岁 的 萧 绮 熙 到 北 京 当 暑 期 助 教 。 她 悄 悄 告 诉 相 熟 的 学 生 ， 想 给 六 四 死 难 者 扫 墓 。 学 生 有 点 惊 讶 ， 但 答 应 同 行 。 萧 绮 熙 手 中 只 得 三 名 死 难 者 的 名 字 和 墓 碑 号 码 ， 墓 地 排 列 错 纵 复 杂 。 她 们 沿 途 向 扫 墓 者 求 助 ， 热 心 人 还 告 诉 她 那 一 夜 的 经 历 ， 「 内 地 人 根 本  忘 记 到 ， 即 使 唔 可 以 公 开 悼 念 ， 公 道 早 在 人 心 。 」（「 内 地 人 根 本 没 有 忘 记 ， 即 使 不 可 以 公 开 悼 念 ， 公 道 早 在 人 心 。 」)<br />
<br />
维 园 的 烛 光 太 远 ， 香 港 年 轻 教 师 萧 绮 熙 04 年 到 北 京 八 宝 山 人 民 公 墓 ， 为 六 四 死 难 者 扫 墓 。 / 萧 绮 熙 提 供 图 片<br />
 <br />
<strong>坟 前 献 花 「 希 望  O 家 人 知 道 」</strong><br />
找 了 好 半 天 ， 萧 绮 熙 终 於 来 到 死 者 坟 前 。 尹 敬 在 木 樨 地 的 家 中 ， 被 子 弹 击 中 头 部 ， 坟 前 已 放 了 鲜 花 ， 「 当 时 心 谂 ， 可 能 有 其 他 人 ， 都  忘 记 到 呢 件 事 。 」 （「 当 时 心 想 ， 可 能 有 其 他 人 ， 都 没 有 忘 记 这 件 事 。 」）吴 向 东 送 院 时 颈 部 受 伤 ， 临 死 前 亲 手 写 下 住 址 交 给 在 场 的 学 生 ， 托 他 向 家 人 报 讯 。 孙 铁 ， 被 子 弹 击 中 胸 部 ； 同 学 筹 款 安 葬 他 ， 墓 碑 刻 上 「 愿 英 灵 永 在 」 。 <br />
<br />
他 们 就 是 香 港 人 多 年 来 手 持 烛 光 默 默 悼 念 的 六 四 死 难 者 。 「 当 时 感 觉 好 平 静 。 」 没 有 悲 恸 丶 没 有 流 泪 ， 萧 绮 熙 低 头 静 默 ， 感 谢 他 们 当 年 伟 大 的 追 求 丶 感 激 他 们 为 国 家 前 途 牺 牲 丶 细 诉 他 们 当 日 的 行 为 ， 影 响 了 她 这 一 代 的 香 港 人 。 她 为 死 难 者 清 理 坟 墓 ， 在 坟 前 放 下 菊 花 丶 剑 兰 ， 「 希 望  O 家 人 见 到 ， 知 道 世 界 上 仲 有 好 多 人 同  O 一 齐 ！ 」 （「 希 望 他 们 的 家 人 看 到 ， 知 道 世 界 上 还 有 好 多 人 和 他 们 在 一 起 ！ 」 ）<br />
<br />
离 开 墓 园 ， 萧 绮 熙 来 到 天 安 门 广 场 ， 浮 现 解 放 军 开 枪 镇 压 的 片 段 ， 听 说 人 民 英 雄 纪 念 碑 仍 留 着 弹 痕 。 走 到 人 民 大 会 堂 ， 那 斑 驳 的 阶 梯 ， 她 想 到 出 三 名 学 生 代 表 周 勇 军 丶 郭 海 峰 丶 张 智 勇 下 跪 求 见 领 导 人 。 「 点 解 要 人 民 跪 低 求 你 ？ 你 系 咪 当 自 己 系 皇 帝 ？ 」（「 为 什 麽 要 人 民 跪 下 求 你 ？ 你 是 不 是 当 自 己 是 皇 帝 ？ 」） 一 幕 幕 八 九 民 运 的 片 段 ， 又 在 脑 内 盘 旋 … … <br />
<br />
<strong>重 看 《 天 安 门 》 哭 声 惊 动 母 亲</strong><br />
她 的 六 四 忆 记 ， 始 於 8 岁 那 年 。 凌 晨 时 分 ， 一 家 三 口 盯 着 电 视 新 闻 ， 小 女 孩 在 狐 疑 ， 电 影 场 面 ， 为 何 会 在 新 闻 报 道 出 现 ？ 妈 妈 怀 胎 六 月 ， 还 是 坚 持 上 街 。 百 万 人 的 征 途 上 ， 她 看 到 最 多 的 标 语 是 「 邓 杨 邓 杨 ， 血 债 血 偿 」 。 但 这 血 债 ， 一 直 未 被 清 算 。 中 学 时 代 ， 老 师 的 讲 解 丶 屠 城 的 片 段 ， 萧 绮 熙 越 想 越 感 动 。 课 堂 上 ， 她 和 同 学 都 哭 成 泪 人 ， 「 学 生 手 无 寸 铁 ， 点 解 要 杀 人 ？ 」 <br />
<br />
中 七 那 年 ， 她 开 始 到 维 园 参 加 烛 光 集 会 。 光 影 晃 动 ， 却 是 令 人 伤 感 的 凄 美 。 支 联 会 主 席 司 徒 华 在 台 上 疾 呼 「 毋 忘 六 四 」 ， 那 激 昂 ， 至 今 仍 在 脑 内 萦 绕 。 <br />
<br />
人 越 成 长 ， 感 受 越 深 。 大 学 一 年 级 ， 正 值 六 四 10 周 年 。 她 参 与 编 辑 六 四 特 刊 ， 撰 写 民 运 日 志 。 翻 看 《 天 安 门 》 等 影 碟 ， 广 场 上 ， 学 生 在 宣 读 绝 食 宣 言 ： 「 在 这 个 阳 光 灿 烂 的 5 月 里 ， 我 们 绝 食 了 … … 国 家 是 人 民 的 国 家 ， 人 民 是 我 们 的 人 民 … … 我 们 不 喊 ， 谁 喊 ？ 我 们 不 干 ， 谁 干 ？ 」 这 一 幕 ， 令 她 再 次 激 动 起 来 ， 哭 声 惊 动 了 母 亲 。<br />
<br />
<strong>北 京 人 没 有 忘 记 　 却 不 敢 提 起</strong><br />
「 自 己 都 是 大 学 生 ， 感 同 身 受 。 」 萧 绮 熙 说 ， 他 们 前 途 本 一 片 光 明 ， 「 点 解 肯 放 低 自 己 ， 唔 理 身 体 去 绝 食 ， 无 私  去 做  多 S ？ 」（「 为 什 麽 愿 意 放 下 自 己 ， 不 理 会 身 体 去 绝 食 ， 无 私 地 去 做 这 多 事 ？ 」） 那 本 特 刊 ， 题 目 是 《 六 四 未 圆 丶 永 未 完 》 ， 「 六 四 一 日 未 平 反 ， 件 事 永 远 唔 会 完 结 ！ 」（ 「 六 四 一 日 未 平 反 ， 事 情 永 远 不 会 完 结 ！ 」） 十 年 前 的 大 学 生 ， 小 时 候 都 从 新 闻 报 道 丶 游 行 集 会 中 感 受 过 民 运 起 伏 ， 唱 K 也 不 忘 《 没 有 烟 抽 的 日 子 》 ， 「 所 有 人 都 未 冷 漠 」 。 <br />
<br />
那 次 扫 墓 後 回 到 香 港 ， 萧 绮 熙 不 时 想 起 北 京 人 对 六 四 没 有 忘 记 ， 却 不 敢 提 起 的 无 奈 。 原 来 ， 可 以 跟 华 叔 (指 司 徒 华) 高 叫 「 追 究 屠 城 责 任 」 ， 可 以 在 烛 光 中 拭 泪 ， 可 以 忘 情 高 唱 《 自 由 花 》 ， 一 切 都 得 来 不 易 。 每 年 一 度 去 维 园 高 呼 「 平 反 六 四 」 丶 在 夜 空 中 高 举 烛 光 ， 对 她 来 说 ， 已 是 一 种 重 要 的 仪 式 ， 不 去 ， 她 会 极 遗 憾 。 好 老 师 ， 更 希 望 每 年 都 带 领 学 生 参 加 烛 光 集 会 ， 将 薪 火 代 代 相 传 。<br />
<br />
<strong>老 师 心 愿 　望 新 生 代 认 清 事 实</strong><br />
八 九 民 运 在 萧 绮 熙 8 岁 时 发 生 ， 小 时 候 对 事 件 的 印 象 很 模 糊 ， 直 至 中 学 时 遇 到 一 位 好 老 师 ， 她 才 渐 渐 醒 觉 。 毕 业 後 ， 萧 绮 熙 也 投 身 教 育 行 列 ， 每 年 此 刻 ， 她 都 会 给 学 生 筹 备 研 讨 会 丶 播 放 纪 录 片 。 <br />
<br />
面 对 不 善 表 达 情 感 的 新 生 代 丶 来 势 汹 汹 的 洗 脑 式 爱 国 教 育 ， 她 只 求 学 生 认 清 事 实 ， 不 会 将 自 己 的 眼 泪 ， 强 行 放 进 学 生 的 眼 里 。 <br />
<br />
新 生 代 全 在 1989 年 後 出 生 ， 成 长 过 程 中 ， 没 任 何 与 民 运 有 关 的 记 忆 。 萧 绮 熙 说 ， 更 困 难 是 年 轻 一 代 不 懂 得 表 达 自 己 的 情 绪 丶 立 场 ， 对 六 四 课 题 如 是 ， 对 爱 国 教 育 也 如 是 ， 「 杨 利 伟  香 港 丶 国 家 队 { 金 牌 ，  O 会 话 开 心 罗 ， 但 系 开 心 唔 代 表  O 爱 国 。 」（「 杨 利 伟  香 港 丶 国 家 队 拿 金 牌 ， 他  会 f 开 心  ， 但 是 开 心 不 代 表 他  爱 国 。 」）<br />
<br />
<strong>facebook 记 录 六 四 记 忆</strong><br />
萧 绮 熙 最 近 在 facebook 内 ， 写 下 她 成 长 中 ， 对 六 四 的 记 忆 。 有 学 生 开 始 对 天 安 门 母 亲 运 动 感 兴 趣 ， 也 有 学 生 追 问 为 何 争 取 民 主 ， 政 府 就 要 杀 人 ？ 她 明 白 到 新 生 代 没 相 同 的 成 长 经 历 ， 不 期 望 他 们 像 自 己 年 轻 时 一 般 激 动 ， 只 愿 学 生 能 不 受 所 谓 的 爱 国 教 育 丶 放 下 包 袱 等 论 调 蒙 蔽 ， 认 清 事 实 。 <br />
