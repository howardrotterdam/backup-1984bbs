他 上 京 时 已 是 5 月 27 日 ， 局 势 却 突 然 平 静 下 来 ， 「 六 四 前 一 个 星 期 好 静 ， 中 国 政 府  人 出  讲 S ， 新 闻 报 道 仲 话 国 务 院  人 听 电 话 。 我 老 窦 仲 问 我 ， &#60811;依 家 上 去 系 咪 水 尾 呀 ？ 」 （六 四 前 一 个 星 期 很 安 静 ， 中 国 政 府 没 人 出 来 讲 话 ， 新 闻 报 道 还 说 国 务 院 没 人 听 电 话 。 我 老 爸 还 问 我 ， 现 在 上 去 是 不 是 尾 声 呀 ？ ）<br />
