6 月 初 ， 第 一 批 娃 娃 兵 入 城 ， 陈 清 华 跟 其 他 民 众 一 起 上 街 拦 阻 ， 「 我 隔 篱 个 人 着 住 套 间 条 睡 衣 去 截  军 人 。 市 民 好 自 发 丶 好 理 性 ， 递 水 畀 军 人 饮 ， 又 同  O 解 释 件 事 ， 叫  O 唔 好 俾 人 呃 。 」（「 我 旁 边 那 人 穿 着 套 条 纹 睡 衣 去 拦 截 军 人 。 市 民 很 自 发 丶 很 理 性 ， 递 水 给 军 人 喝 ， 又 跟 他 们 解 释 这 件 事 ， 叫 他 们 不 要 被 人 骗 。 」 <br />
