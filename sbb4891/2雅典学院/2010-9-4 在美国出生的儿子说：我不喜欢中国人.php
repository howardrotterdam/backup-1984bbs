																											<div id="postmessage_812709" class="t_msgfont">在美国出生的儿子对我说:我不喜欢中国人<br />
<br />
:作者:怀念狗宝宝 提交日期:2010-08-23 00:49<br />
　 　 <br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp;&nbsp;&nbsp;今天放学接了儿子，看到小家伙虎了张脸，问他什么也不肯说。在回家的路上，他突然冒出一句：我不喜欢中国人。我大吃一惊，深究到底是什么原因使得一向脾气温和的儿子下如此断语？原来，学生们在下课之后围聚在一起看YOUTUBE，其中有一缉是关于中国饲养黑熊活取胆汁的记录片，据儿子说，不管白人黑人墨西哥人，个个看得热泪盈眶。看完之后这些十一二岁的少年高喊‘中国人该死’。这个学校没多少中国学生，儿子受到刺激之深是可以想象的。 <br />
<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　我是看过那缉影片的，那些被关在不能转身的笼子里的黑熊每天都被粗大的针管刺入腹部，抽取胆汁，那种痛苦是非能用言语来表达的。更为甚的是，这是一个有组织的盈利生产线，这些黑熊的痛苦是无穷无尽的，直到死亡为止。很多黑熊受不了这种痛苦，自行撕开肚腹了断。生存下去是所有生物的本能，我曾亲眼所见一只被捕兽器挟住的狐狸自行咬断腿而逃生的场景。如果出现了上述黑熊自杀的情景，只有两个解释；第一是这种残酷行为超过了自然界生物所能承受的界限，第二是施行这种残酷手段的人类走过了自然界规定的大限。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　我试着跟儿子沟通；那只是某些人的个别行为，并不代表所有的‘中国人’。儿子坚决地摇头：这种事情太可怕了，我们每个中国人，包括他这个从来没踏上中国土地的孩子，都不可能不为此种恶行担上骂名。我没有理由反驳他，正如我们受到某个族裔个别人的恶意侵犯，我们也会对整个族裔抱有看法一样。个人是细胞，族裔是整体，你没办法问：为什么这个整体会产生出这种恶性细胞来？就算你理性上给自己解释通了，你深层的潜意识上还是会留下一个模糊但深刻的印记；这个族裔曾有人做出如此残酷的事情来。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　我不敢跟儿子说的是；我们中国人，一直有这种为了口腹之欲，而残杀动物的传统。广东人吃猴脑，把活生生的猴子放在有孔的桌子上，敲开头骨，直接用勺子取食，垂死的猴子不停地挣扎，击响桌下的鼓。食客们还觉得此乃助兴之举。我们知道，猴子和人类的基因基本相同，我们有的痛感和恐惧感它们也有。近年来大陆某些地方流行活剜驴肉，客人指定哪一块，厨子就从驴身上直接割下来烹饪。以前还有为了吃新鲜的鹅掌，赶着鹅从滚烫的铁板上走过。欧洲的金毛犬是极为温和，极其与人亲近的一种犬类，一直是导盲犬的首选，却被引进中国成了香肉煲的材料。直到今天，熊胆究竟有什么医药作用还在未定之天，猴脑也不可能把傻瓜吃成天才，谁也不能论证活剜的驴肉到底比正常屠宰了的动物美味到哪里去，中国现在也不是非常时期，副食品供应丰富，并不缺了那点金毛犬的蛋白质。但是我们就敢冒天下大不韪，为了逞一己之欲，不必要地残杀，虐待动物，这些跟我们生存于同一个地球上的动物。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　我更不敢跟这些孩子们说的是：我们中国人发明了世界上最无可比拟的残酷的刑罚，那是用来对待跟我们一样的同类，煌煌五千年的文明史，翻开来却充斥了抽筋剥皮，车裂腰斩，妇女坐木驴，三千多刀剐杀活人的记载。汉代吕后砍去情敌戚夫人的手脚，剜眼割舌，扔在粪坑里，名曰‘人彘’。就是到了近代，留学法国的政治人物一样把对手满门抄斩，连吃奶的孩子都不放过。在文化革命中，十二三岁的女红卫兵，敢在光天化日下用皮带打死无冤无仇的老妇人，只因为她是所谓的‘地主婆’。如果一个族裔如此残忍地对待同类，而且在历史上一再出现，从未反省，从未被苛评，从未羞愧，从未为戒。那么，永劫回归，不管文明发展到哪个地步，可以料想人们还是会如此残酷地对待同类，对待动物。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　你再解释也没用的，那些十二三岁，睡觉时还把长毛绒的熊宝宝放在枕边的小孩子，天然而敏锐，当他们亲眼看到活生生的残酷画面，听到黑熊没命的惨嚎，你叫他们怎么冷静地分辨一小撮和大多数？那些饲养黑熊取利的人无名无姓，但作恶者是中国人却一目了然。你再说西方人怎么敬仰我们的五千年文明，毕竟是抽象的，但是亲如家中成员，天天做伴，温驯的金毛犬落入被人宰杀食用境地，不可能不使听者哗然。这种恶劣印象是不可能轻易改变的，无论海尔冰箱或张艺谋的电影都无能为力的。再过几十年，这些孩子有可能是美国的政策制定者，我相信，那些黑熊悲惨境遇一定会伴随他们很久很久，成为外交关系，人际关系中一个触摸不到的隐机。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　一个真正在哲学上，文明上成熟的族裔首先是在生死观上的成熟，体现在对所有的生命持有广义上的悲悯。丰富的物质和所谓的形象工程都在其次，人性的残忍使得这一切都蒙上阴影。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　别说你和我没用过熊胆产品，作为这个族裔中的一份子，我们同时负担着这个群体的荣光和耻辱，如果你我不为这种虐杀行为深感羞愧，那么，我们所谓的文明――那件华美的长袍下的虱子还会继续繁殖下去，只要特定的时机来临，我们还是会一成不变残酷地对待同类，还是会继持地虐待和残杀动物，遗祸后代，遗祸我们所居住的世界。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　是的，作为一个中国人，面对那些虐杀黑熊的画面，我深感羞愧。</div>

							
							
							
							
