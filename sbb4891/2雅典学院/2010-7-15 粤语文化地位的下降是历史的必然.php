																											<div id="postmessage_741603" class="t_msgfont">提到粤语的“保护”，我认为是常见的词语误读，而被一些人曲解的。<br />
<br />
我理解“保护”派实际上要表达的意思是“保护粤语的地位”，而非“保护粤语不被消灭”。不同的语言有地位高低，我想大家是有体会的，应用领域不同。在广东，粤语作为中心语言，是有相当高的低位的，即使普通话在学校里面普及，也只作为一种外来语，因为难以传达广东本地文化。<br />
<br />
另一方面，我觉得这是一次话语权的实践，不论媒体和个人都有地方讨论发表意见。所以我表示支持。但同时我觉得这样的话语权也是一种悲哀，地方政府的财政还没被问清，官员财产还没被查清，司法暗箱操作没有被曝光……那么多需要众多市民话语权的地方却无人问津。<br />
<br />
所以说广东人还是注重实质，粤语的地位可能会影响生活便捷，粤语地位被动摇就会用话语权来捍卫，但关于政治体制那些，对太多人来说就太虚了。</div>

							
							
							
							
