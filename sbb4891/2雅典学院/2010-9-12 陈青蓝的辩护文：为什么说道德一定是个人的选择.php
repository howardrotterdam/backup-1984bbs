举第三个例子，在19世纪的印度，寡妇为死去的丈夫殉葬被大多数人认为是必须遵守的道德。如果一个寡妇不愿意为丈夫殉葬，那么她就会遭到全国人民的辱骂。 你会认为这种辱骂是正确的吗？ <br />
<br />
康德说：没有自由就没有道德。自由是道德的前提。<br />
<br />
道德一定是个人的选择。正常社会，价值观是多元的，每个人都有权选择自认为是善的道德而遵循。我们可以说，在统计学意义上选择某种道德的人比较多，但不能说有什么“社会道德”。这就好比选择吃麦当劳的人比较多，但不能说麦当劳就是“社会食品”。<br />
<br />
另外，说道德一定是个人的选择，还因为为选择某种道德付出代价的一定是其本人。你选择某种价值观和道德观，就可能因为与那些不同意你的价值观和道德观的人疏远，从而失去与他们交往所带来的利益。这种代价不可能由别人来承担，而只能由你自己来承担。举个例子，你开一家餐馆，声称不允许妓女进来吃饭，那么你就会失去这部分生意，同时，你也可能失去那些对你的道德观反感的人的生意。你奉行不和妓女做生意的道德观，失去的生意就是你付出的代价，这个代价别人是无法为你承担的。正因为此，别人对你的道德观的指责就毫无道理。<br />
<br />
道德是拿来律己的，而不是律人的。选择什么样的道德，是个人的权利，别人无权指责。那些拿“社会道德”来要求别人的，自己不用承担代价，而只是在谴责别人的过程中满足自己的道德优越感而已。<br />
<br />
那么，这个社会没有底线了吗？有。底线就是你的个人选择不要侵犯到别人的权利和自由。这就应该是正常社会的底线。而这部分底线是通过法律来规范的（这里所说的法律，不是指政府随意设立的法规，而是指人类社会自发演进所形成的自然法。政府设立的恶法不能称为法律。）那就不属于道德的范畴了。<br />
<br />
<br />
<br />
<br />
网络评论：道德本身是合作规则，类似一种商业模式。个人可以选择不采用这样的商业模式的合约形式，但也没有必要贬低这种形式。自由主义不是道德虚无主义。尤其不能对自己有利时就遵守道德，对自己不利时就要求别人不要用道德批判自己。<br />
