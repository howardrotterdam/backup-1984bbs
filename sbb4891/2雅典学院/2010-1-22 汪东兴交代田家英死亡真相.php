													<h2>汪东兴交代田家英死亡真相</h2>
						
						
																											<div id="postmessage_446632" class="t_msgfont">汪东兴交代田家英死亡真相&nbsp;&nbsp;<br />
<a href="http://caopeizhi.blog.hexun.com/29138167_d.html" target="_blank">http://caopeizhi.blog.hexun.com/29138167_d.html</a><br />
&nbsp; &nbsp; 田家英，四川成都人。<br />
<br />
&nbsp; &nbsp; 1936年在成都参加抗日救亡工作。次年参加中华民族解放先锋队。<br />
<br />
&nbsp; &nbsp; 1938年入陕北公学学习。同年加入中国共产党。曾任延安马列学院教员、中共中央政治研究室研究员、中共中央宣传部历史组组员。<br />
<br />
&nbsp; &nbsp; 1942年1月8日，田家英在《解放日报》发表的杂文《从侯方域说起》，受到毛泽东的好评。1948年，经过胡乔木的推荐，田家英到毛泽东身边当秘书，直至1966年。<br />
<br />
&nbsp; &nbsp; 1954年后，兼任国家主席办公厅、中共中央政治研究室、中共中央办公厅副主任。是中共八大代表、第三届全国人大代表。著有《学习〈为人民服务〉，宣传毛泽东思想》、《中国妇女生活史话》，编有《民国以来大事年表》等。参加了《毛泽东选集》第一至四卷的编辑出版工作，曾言“毛泽东思想己渗入到我的骨髓里”。<br />
<br />
&nbsp; &nbsp; 1958年北戴河会议上，田家英被点名批判为“右倾”。<br />
<br />
&nbsp; &nbsp; 1959年庐山会议后期，田家英被揭发有“右倾”倾向和言行。会上曾表示，他离开中南海的时候，准备向毛泽东提3条意见：一是能治天下，不能治左右；二是不要百年之后有人来议论；三是听不得批评，别人很难进言。<br />
<br />
&nbsp; &nbsp; 1960年代初，田家英向毛泽东建议起草农村工作条例，后参加起草《农村人民公社工作条例（草案）》，即农村工作“六十条”，对纠正“大跃进”运动以来农村工作中的左倾错误产生了积极作用，毛泽东曾说：“'六十条’的发明权在田家英。”后还曾向毛泽东建议“包产到户”，并起草《恢复农业生产的十大政策》，遭到批评。<br />
<br />
&nbsp; &nbsp; 1965年12月，田家英被安上“篡改毛泽东著作”的罪名。<br />
<br />
&nbsp; &nbsp; 1966年初，田家英曾对一位朋友说：“林则徐的诗句'苟利国家生死以,岂因祸福避趋之’。这是我的人生座右铭。真理我要坚持到底。直到停止呼吸，绝不出卖灵魂，绝不向错误屈服。”<br />
<br />
&nbsp; &nbsp; 1966年5月22日，田家英被逐出中南海。5月23日，死于中南海永福堂。死因有争议，一说上吊身亡，一说开枪自杀，一说他杀。<br />
<br />
&nbsp; &nbsp; 1980年初，田家英冤案得以平反昭雪。<br />
<br />
&nbsp; &nbsp; 1980年3月28日，田家英追悼会在北京八宝山公墓礼堂举行，悼词中说：“家英同志是一位经过长期革命锻炼，忠于党、忠于人民，有才学的优秀共产党员。他为共产主义事业努力奋斗，作了大量的工作。”“几十年的实际行动证明，家英同志确实是一个诚实的人，正派的人，有革命骨气的人。他言行一致，表里如一。他很少随声附和，很少讲违心话……” <br />
 <br />
 <br />
中央一直宣称，MZD的政治秘书田家英是一九六六年五月自杀身亡的。据中共中央组织部（外调一九七八－七－００六六一二）号档案记载：田家英接到中央要来人和他作最后一次挽救性谈话的电话后，约一小时在寓所永福堂，用五四型手枪开枪自杀的。枪声惊动了警卫。汪东兴接获警卫报告后，和内卫朱国华赶到现场，证实田家英已死。汪东兴报告主席请示处理意见，主席说：死了人都要我管吗？他选择了自绝于人民的道路。又请示总理，总理说：是自杀吗？要送医院验尸，这是法律手续。后又说：按主席的指示办。当天下午，是以化名，由公安部出面送去火化的。<br />
一九八０年三月，田家英终于获得了平反昭雪。<br />
<br />
胡耀邦、邓小平、陈云、彭真都就田家英家属来信的要求，做出批示。其中指出：田家英是自杀吗？为什么不送验尸？为什么有关档案会失落？田家英有关资料的去向如何？朱国华为什么自杀？对这些问题，汪东兴要有个如实交代。<br />
<br />
一九八０年四月一日，由中纪委常务书记黄克诚、中办副秘书长姚依林，代表中共中央、中纪委，就田家英死亡问题，找汪东兴谈了话，给予汪东兴选择如实交代田家英死因和有关档案失落原因的机会，他的选择将直接关系到汪本人的下场。<br />
<br />
汪东兴当时先硬后软，冲着黄、姚二人说：没想到清算到主席身上，连我现在什么职务都没有了也不放过。<br />
<br />
黄克诚说：汪东兴同志，我们不是来跟你做交易，我们是代表党、纪委，给你机会。历史是不能颠倒的。田家英是自杀吗？这，你不清楚吗？最后，黄克诚，姚依林限汪东兴在三天时间内作出回答，反思。<br />
<br />
一九八０年四月四日，汪东兴向中央办公厅提交了对田家英死亡事件的交代报告。<br />
<br />
该报告被列为中共中央档案局〔一九八０―四―００九０一四〕号档案，附有备注：待查证。并有中纪委第一书记陈云，常务书记黄克诚的签字。<br />
<br />
该档案的汪东兴笔录摘要如下：<br />
<br />
我接主席指示，要我到田家英寓所能否跟田谈一下，不要走到反的一方，不能自拔。五月二十三日上午十时，我和内卫到永福堂田家英寓所，向田传达了主席的关怀，问他有什么想法？田冷漠地笑了一下，说：十八年了，我思想一贯右倾，一贯反马克思主义，一贯和主席唱反调，一贯和彭德怀、彭真、杨尚昆搞在一起。主席为什么留我在身边？我问田：还有什么想法？田说：不能违背事实讲话了。彭德怀、彭真、杨尚昆，还有一批和主席南征北战的领导同志，他们怎么会是反党，反马克思主义？让历史见证。我说：不要执迷不悟，否则下场比彭德怀、彭真、杨尚昆还要惨！田家英立即回答：请报告主席，我田家英对主席了解了，主席也会对我田家英了解。我接受还要惨的结局。说着，田家英把杯子用力朝地一摔，表达了他和主席的决裂。当时气氛激烈，内卫朱国华失控，朝田家英开了一枪。我即招呼警卫上前一看，田家英已死，随即向主席、总理作了报告。主席听后说：人死了，不能复生。他在我身边十八年了。从五六年以来，历次政治大事件中，他都有主见。我把他作为右派秘书。后来，他和彭（德怀）、彭（真）、杨尚昆搞在一起，性质就变了。他对另一个主席很尊敬，他也有野心。<br />
<br />
内卫朱国华后自杀。<br />
从汪东兴这份如实程度可疑的交代材料，也仅能证实田家英不是自杀，而是他杀，但他杀的内幕仍是一团迷雾。问题是谁下的指示，究竟是内卫开的枪，还是汪东兴开的枪？内卫朱国华的死因，以及当年有关毛泽东对此一事件指示的档案材料为什么会失落呢？<br />
<br />
