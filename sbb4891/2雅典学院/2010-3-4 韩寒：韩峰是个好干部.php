最近广西烟草部门韩峰局长的日记很热门，我认为在艳照和视频充满网络的年代里，突然来一些真人纯文学的描写，如沐春风。这应该是2010年最具文学价值和社会价值的文学作品了。我看了局长日记以后，觉得我们不应该讨伐这位局长。我们假设局长日记日记是真的，我敢说这其实是一位好干部。<br />
<br />
 <br />
<br />
1，&nbsp;&nbsp;这位干部在半年的时间内仅仅受贿6万元，这是我近几年第一次看见受贿两次后面跟的是一个5位数，现在哪里去找这么清廉的局长。<br />
<br />
2，&nbsp;&nbsp;在他已玩，正玩，稳定的玩和待玩的那些床伴中，无一人属于包养性质。<br />
<br />
3，&nbsp;&nbsp;这位局长不赌博，不嫖娼。在公共场合办手机卡的时候按顺序排队，一排就是两个小时。<br />
<br />
4，&nbsp;&nbsp;在他的日记里，我们看到了一个以最小的成本在泡妞的国家干部，在其他干部都送二奶房子车子的时候，他送给自己的女人最贵重的礼品仅仅是一个手机或者一个MP4。<br />
<br />
5，&nbsp;&nbsp;他应酬喝酒仅89次，我知道的大部分的村干部一年喝酒应酬都要超过365次。但是他多次喝醉，酒量一般，这点没有达到一个国家干部的要求。<br />
<br />
6，&nbsp;&nbsp;虽然他玩女人多次，但是陪老婆也有25次，给父亲购买手机，其中也没有发现利用职务之便给自己亲戚走过后门。<br />
<br />
7，&nbsp;&nbsp;他会自己安装电脑软件，爱好数码，爱好摄影，爱好体育，用微博体写日记，属于一个比较与时俱进的领导。<br />
<br />
8，&nbsp;&nbsp;在他的日记里，完全没有看到一个国家干部对于豪华汽车，房产奢侈品，书画古董收藏所表现出来的应有追求，甚至连个念想都没有，只是默默的玩着他的手机和电脑。在日记里，他甚至写道，今天买了一个160元的XX耳机，真好！多么知足常乐的干部。<br />
<br />
9，&nbsp;&nbsp;至于工作方面……虽然尚且没有看出这位领导进行了任何的工作，但是所谓干部干部，只要干了自己的部下，也就完成了这个这个名词赋予的意义。<br />
<br />
 <br />
<br />
综上所诉，在现有的国情下，他绝对是一个标准线以上好干部，这样的干部属于自娱自乐容易满足型，对国家和人民造成的危害是最小的。我们看到了一个仅仅几个千把块钱就能让他乐不思蜀的国家干部，当他买了一部手机后，连续三天的日记内容都是“在家玩手机”，连女人都不玩了。在他这个位置上，比他罪大恶极的干部多的是。我强烈的建议网名们放过这位干部，让他继续在他的工作岗位上研究他的数码产品，如果他下台了，接替他的人很可能比他对社会的危害要大很多，唯一的区别就是人家不写日记。<br />
<br />
我是认真的。</div>

							
							
							
							
