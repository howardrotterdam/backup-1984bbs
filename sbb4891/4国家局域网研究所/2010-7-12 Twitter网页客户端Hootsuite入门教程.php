<strong>Search</strong><strong>下</strong>，可以对某一关键词进行搜索，如果搜索的关键词是汉字，每两个汉字之间要留一个空格。比如，假使你想搜索提到“老罗”的推，就应该在此填入“老 罗”。<br />
<strong>Keyword</strong><strong>下</strong>，关键词监视，和上面效果一样，不同的是，这里可以最多同时监视三个关键字。监视汉字的规则也同上。<br />
<strong>Lists</strong><strong>下</strong>，只要你在Twitter官方建有lists，就可以他们添加为这里的栏（a stream）。<br />
9、&nbsp;&nbsp;下图为Hootsuite上的发推框。&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; <br />
<img src="http://www.bullock.cn/blogs/liulei/files/8.bmp" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<strong>Shrink</strong>是缩短长的网址用的。Hootsuite里可以使用两种形式的缩短网址，<a href="http://ow.ly/url/shorten-url" target="_blank">ow.ly</a>和<a href="http://ow.ly/url/shorten-url" target="_blank">ht.ly</a>，可以在Preferences里改。<br />
<strong>“回旋针”</strong>是上传图片（如，<a href="http://img.ly/" target="_blank">im.ly</a>）、文件用的。但Hootsuite里经常不能正常上传。<br />
<strong>“小日历”</strong>用于定时发推（Schedule Messages）。比如，你明天去泰国，十点的飞机，你可以把“在去泰国的飞机上，到了泰国就可以直接上Hootsuite咯！”这一推定于十一点发。在十一点之前，该推将被保存到Pending Tweets一栏里。<br />
<strong>“小软盘”</strong>用于保存已经写好却不想马上发的推。保存后，可以通过点击后面的小箭头调出草稿。<br />
