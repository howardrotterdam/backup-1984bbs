<img src="http://www.bullock.cn/blogs/liulei/files/9.bmp" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
这里主要解释一下Settings里的各项。别的，对于像我这样的普通用户，就不太常用了。<br />
<strong>Social network</strong>里的内容基本用不到。只有当你想删除已添加的Twitter账户时，才能用到<strong>Profile Setting</strong>里的Delete Profile功能。<br />
<strong>Rss/Atom</strong>可以用来定时同步博客的标题。<br />
<strong>Account</strong>里可以对你注册Hootsuite的一些信息进行修改，如注册名、邮箱地址、时区等。<br />
<strong>Preferences</strong>里可以对Hootsuite所使用的默认语言（只有英语和日语）、默认缩短网址及RT类型（可编辑的非官方RT或不可编辑的官方RT）。同时也可以选择“在有新的推时，是否自动发出声音提示”。<br />
<strong>Themes</strong>里有三种主题可供你选择。<br />
