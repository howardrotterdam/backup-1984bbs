													<h2>转下目前新疆人翻墙的方法</h2>
						
						
																											<div id="postmessage_609573" class="t_msgfont">一、断网<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 现在新疆全境依然是断网断短信断国际长途三断中。这种中断跟内地的GFW不尽相同，对于普通用户基本上算是物理隔离，几乎所有外网ip和外网端口均无法访问。能够访问的只有疆内的站，以及极少数ip和主机在外地的国家级官方网站（如网站备案查询、各种考试报名网站）。也就是说，国内公众网的GFW是默认放行，部分ip和域名拦截+关键词拦截；而新疆是默认断开，个别ip和域名放行。<br />
&nbsp; &nbsp; 上网方式方面，7月6日凌晨4点chinanet全部封锁，随后是移动、联通的公众网络。教育网（CERNET）估计是用的电信出口，也是同步被封。科技网（金桥）多撑了两天，于7月9*被封锁。手机上网方面，NET方式均同时被封锁，WAP方式则推迟到七月十几*左右才封锁，其中电信的WAP方式甚至到了 8月中才封锁完毕，期间一直可以登录WAPQQ。3G上网卡方面，基本是同时封锁，有传闻是如果用天翼3G拨号时获得的IP是120.X.X.X就可以顺利上网，不过这个传闻未验证。普通用户能够翻出GFW终极版的上网方式只有拨号一种了，也就是10年前我们大家常用的56k窄带拨号。具体方式后文详述。<br />
&nbsp; &nbsp; 短信方面，点对点短信均无法发送，手机端直接报错，应该是点对点短信网关根本就没启动。仅有部分公众SP业务开通，如天气预报、手机报、 10000/10086/10010这类的运营商官方信息，用户可以接到。需要注意的是外地手机漫游至新疆，一样无法发短信，所以不要妄想买个外地短信卡就能在新疆聊天了。我自己曾经发短信如飞的手指，现在也快不知道怎么按了。可以说这几个月新疆的SP和CP类公司遭到了重创，倒闭关门或撤回内地的不计其数。<br />
&nbsp; &nbsp; 国际长途方面，大概是7月7*开始就非常难以拨入，8月以后就基本无法拨入了。呼出方面，除了当局指定的仅有几个机关单位和几个大的电信营业厅可以打（像不像80年代），其他全部的直播、IP、网络国际长途电话均无法呼出。批发国际ip卡的商家亏到出屎，新疆大批做中亚外贸的商家也都难以开展业务。我的表妹在马来西亚上学，快半年了没一个电话，她老妈被逼的请了假去内地上网，终于在QQ上得以一见。<br />
&nbsp; &nbsp; 7.5后的这些封锁，除国内长途封锁时间较短，7.5后几天即解除外，其他的直到现在（12月25*）也没有一点儿放松的迹象。基本每个月都有传闻说这个月/下个月要开网，什么时候开网也是疆内各论坛最常讨论的一个话题，已经不能用*经来形容了。可惜这些期望，最后都是以失望告终。<br />
&nbsp; &nbsp; 其中有这么几个著名的传闻：<br />
&nbsp; &nbsp; 断网持续48小时：这是7月6*最初的传闻，据说来自于运营商。可惜随着7月7*汉族大规模游行示威告吹。<br />
&nbsp; &nbsp; 断网一周：参照石首，这个传闻很快告吹。<br />
&nbsp; &nbsp; 断网半个月/一个月：说西藏314时断网就这么久，新疆也会参照。可惜据我本人考证，西藏当时似乎没有断网，或断网时间很短。在Google上搜“西藏 断网”，貌似没有什么有价值的信息，希望有了解情况的JR说说。<br />
&nbsp; &nbsp; 10月国庆后开网：这个传闻是说都60大寿了，安定和谐的天朝肯定会给我们这2000w*民开网。告吹。<br />
&nbsp; &nbsp; 11月1*开部分门户网站，春节放开全部：这个传闻是最有模有样的，据说是自治区级**安排，大部分运营商都内部通报了。可惜现在马上新年了，仍然没有一个门户网站能够访问。这个传闻应该为真，据说最后是自治区政法委书记符强同志强力压了下来。<br />
&nbsp; &nbsp; 最新的传闻是说1月份要开放部分门户，全部放开到2010年5月份左右：这个消息同样来自运营商内部，然则已经没有多少人关心了。对通网的传闻新疆的网民早已麻木，对这类消息都是无奈的一笑了之。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 二、翻墙<br />
&nbsp; &nbsp; 下面来说说大家最关心的，那些能上网的新疆人，是怎么从铁桶一般的墙里翻出来的。<br />
&nbsp; &nbsp; 首先，内地公网使用的翻墙方法完全不适用于新疆，因为封锁的原理不同。新疆现在就是一个大局域网，外网根本就不通，你提供哪些国外的翻墙网站或者翻墙软件，自然只会404 not found或者service unveilabe。现在已知的是如下几种方法：<br />
&nbsp; &nbsp; 1、窄带拨号：<br />
&nbsp; &nbsp; 这个是应用最广泛的拨号方式，你不需要有特权，也不需要上面有人，只要你有一个破旧的56k窄带猫，连上电话线就可以。虽然速度很慢，开个新浪都要等2分钟，但是，那是外网啊（请自行脑补加入流泪233表情）！当然，你不能拨本地的窄带接入号，那一样只能访问疆内网。<br />
&nbsp; &nbsp; 流行的拨号号码有这些：<br />
&nbsp; &nbsp; 022-16300、0891-16300：8月起，这两个号码造福了很多网民。稳定，但不容易拨入，平均拨号5次能够连接上，10月份被封。<br />
&nbsp; &nbsp; 010-95700：10月到12月间，这是最流行的拨号接入号，拨通率非常高。如果你看到有人家里固话电话费突然升到好几百元甚至上千元，那不用问，肯定是天天95700呢。这个号码12月初被封。<br />
&nbsp; &nbsp; 0756-96169：95700不在的日子，我们又找到了96169。可惜没能坚持1个月就被封了。<br />
&nbsp; &nbsp; 友情提醒：拨长途窄带ISP号码前请加拨11808，这样一小时话费能够控制在5元以内，否则请按长途标准资费（大约1分钟7毛钱）换算自己的电话费。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 2、企业内网：<br />
&nbsp; &nbsp; 大家知道很多大型企业的内部网络都是全国连通的，电信运营商自不必说，其他包括铁路、石油、银行等都有自己的全国性网络，由于各企业的业务需要，这些内网是不可能断掉的。这就给通过内网翻墙带来了可能性。只要在其他省份找到一台同在内网且可以上网的机器，于其上开个代理，便可以顺利上网。一时间，“内地同事 ”成了抢手货，疆内软件网站上ccproxy、wingate这些代理软件也都上升到下载榜前列。<br />
&nbsp; &nbsp; 这种翻墙方式网速尚可，实现难度也不算太高，也算是比较流行的翻墙方式。当局下强硬命令要求各单位逐机清理的7-5视频，大多也是这种方式流传出去的。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 3、海事卫星等卫星通讯方式：<br />
&nbsp; &nbsp; 卫星通讯方式的特性决定了它不可能像有线网络一样被地区性中断，有条件的单位和个人，可以通过这种方式翻墙。不过这种方式网速不怎样，而且资费极贵，所以基本只有少数单位在使用。<br />
&nbsp; &nbsp; 有次翻墙出来上QQ遇到一个疆内的朋友，问他怎么上网的，便给我炫耀其单位特权申请了卫星专线4条，办公室电脑都能上网了云云。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 4、特权<br />
&nbsp; &nbsp; 在中国，任何事情的限制范围，都可以加一条：**例外――就连断网都断得这么有中国特色。除了通讯管理局、公安厅、安全厅这些跟互联网直接有关的单位可以上网外，据我所知还有各通讯运营商处级以上**，当局机关的部分部门，疆内官方大站的运营部门（新丝路、天脉、天山网、亚心网、乌鲁木齐在线），均可以顺利上网。当然，各单位都有相应的管制手段，如限制部分端口，限制QQ登录等，但还是让普通百姓羡慕不已。<br />
&nbsp; &nbsp; 当然，普通企业和个人“原则上”也可以申请开通，但除非你能通过电信运营商-通讯管理局-公安/安全/自治区当局多个部门层层审批通过才行，至今通过的公司寥寥无几，个人更是不要奢望了。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 5、异地上网方式<br />
&nbsp; &nbsp; 之所以新疆能被这么彻底的断网，跟新疆的地理位置也有很大关系。如果内地的某个省份出事被断网，网民很容易就能到别的省去，而且内地错综复杂的线路也不是能说断就断的。新疆的骨干网出疆端口只有两条，很容易就能封锁。如果你想出去外省，哪怕是到最近的甘肃，那一千多公里的路程至少也得1整夜的火车。<br />
&nbsp; &nbsp; 但即便这样，仍然有难以忍耐的网民跑去外省上网。BBC就报道过一个新疆人坐飞机去深圳上网的事情。而新疆本地QQ上流传的一条信息则是：“柳园一出车站就有好几家网吧，我去过了。网吧对面就是旅馆，下火车出站就可以上网了。那个网吧全是新疆这边的，本地人基本没与几个，到了晚上8点全是新疆的网民。包夜 8 块钱。10点到早上9点。就这些了。过年时候我也要去 我的电话136699xxxxx要去一起呵呵！”――柳园是甘肃省离新疆最近的一个小火车站。<br />
&nbsp; &nbsp; 还有一则未经证实的消息，是说有人利用手机基站信号的边界重叠，去新疆东南边的若羌县用青海省的3G信号上网。之所以说未经证实，是因为那里是维族人口占90%以上的民族聚居地，就算能3G上网，但小命不一定能保全啊……<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 以上基本就是新疆境内翻墙的方法，发出来给对网络习以为常的里层JR们看看，新疆人民上个网是多么难。有的人QQ密码忘了，有的人苦心经营的网站废了，连菜地都快半年没收成了……去内地出差回来的同事说：“你知不知道当我输入三达布溜淘宝点康木一回车，看到那熟悉的黄色页面出现时，才真正的体会到了什么叫这一刻我内牛满面……”<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 三、现状<br />
&nbsp; &nbsp; 断网前新疆 90%以上的互联网流量都是流向内地的，但现在这个大局域网已然成了私服和山寨网站的乐土。各种网游私服层出不穷；利用FICQ和RTX搭建的新疆QQ也有N个版本了；山寨开心网不下10个，虽然用的都是同一套源代码连界面配色都一模一样；hao123类的疆内网站导航已知的就有几十个；甚至连百度和 Google都被山寨了（参见附图），虽然做的有够烂……最近最火的，则是一个叫zn11的网站，点击率超级高。这个网站是干什么的呢，说出来外面的各位可能会哑然失笑――就是用teleport把新浪、腾讯、网易等几个大站的首页和2、3级页面离线下载，然后挂到站上。虽然可能几天才更新一次，大量链接点不开，视频也无法收看，但还是有很多人上班第一件事就是打开这个zn11。也许是让自己觉得离真正的互联网近一点吧……<br />
&nbsp; &nbsp; 这是一个最好的时代，这是一个最坏的时代。对于私服和山寨网站运营者来说，显然是前者。但对于电信运营商来说，很有可能是后者。虽然广播报纸上电信移动联通三家的3G广告还是铺天盖地，但是谁都会对“WCDMA，上网速度可达7.2兆”的广告一笑了之，网都没了推广什么3G啊。损失最重的是主要收入来自于宽带业务的新疆电信，7月份全疆宽带费用全免，一个月直接损失5000多万，关联损失8000多万。8月份以后宽带费用采用用多少交多少不用不交，最多收取原费用8折的方法，但已然抑制不住每月飙升的宽带拆机量了。<br />
&nbsp; &nbsp; 上不了网的不方便，很多人已经渐渐习惯了。官方大站上不少“没有互联网的*子，我过得更好了”这类的傻逼帖子，甚至我朋友里也出现了斯德哥尔摩综合症患者。但我想大家更为担忧的，应该是TG在观察过新疆百姓对三断的反应之后，会不会把这个损招用在其他省份，甚至全国。<br />
