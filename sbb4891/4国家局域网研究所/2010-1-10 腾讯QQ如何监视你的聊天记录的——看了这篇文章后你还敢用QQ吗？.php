																											<div id="postmessage_423170" class="t_msgfont">腾讯QQ如何监视你的聊天记录的――看了这篇文章后你还敢用QQ吗？ <br />
可怕呀 腾讯QQ如何监视你的聊天记录的<br />
<br />
杭州的徐小姐是腾讯QQ的老用户了，不过，最近发生的一宗QQ号码被封事件，开始让徐小姐变得警觉、惊恐直至愤怒了。<br />
<br />
(一)事件――QQ号码被查封<br />
<br />
6月里的一天，徐小姐像平时一样打开电脑上网，登陆QQ时，突然弹出一个窗口，提示“禁止使用”，徐小姐试了几次，仍然是这样。徐小姐后来回忆说：“我先是通过电子邮件询问，没人理我，一个星期后，我终于忍不住打长途电话到深圳，问腾讯的客服，要她给我一个号码被封的理由。客服几分钟就查到了，说我下载了有关政治性的敏感文档，是有记录的，所以号码才会被查封。”<br />
<br />
记者在征得徐小姐的同意后，即以该QQ号码的使用者身份致电深圳腾讯公司，了解号码被查封的情况，在报出QQ号码和密码后，该客服人员让记者稍等，几分钟后，客服人员告诉记者，这个QQ号码在5月30日下载过敏感的信息，所以被查封，而且，“不可能要回来了”！<br />
<br />
按照徐小姐的回忆，她的确是曾经下载过一个文档，“如果不是腾讯有记录，我都想不起来了。可我连看都没有看全，又没有散布，这也要封号码？”更奇怪的是，徐小姐当时并没有使用腾讯的TE浏览器下载该文档。<br />
<br />
徐小姐怀疑，难道只要装了QQ就可以监视用户的电脑使用情况，并且可以把用户电脑中的资料回传到腾讯的服务器上？如果这是这样，那么，还有什么个人隐私可言？况且作为QQ会员的徐小姐，她本人的身份证号码、手机号码都是按照腾讯公司要求记录在案的。<br />
<br />
其实还不仅仅是涉及隐私问题这么简单，如果政府部门或者商业机构中有人在电脑中装有QQ，就有可能造成严重的信息外泄的安全隐患了。QQ捆绑的TE浏览器提供的“谁与我同在”的功能，就可以追踪和了解QQ用户访问了那些网站。<br />
<br />
(二)调查――用户信息被记录<br />
<br />
一位在软件安全问题方面颇有研究的业内人士向记者证实，通过Iris抓包软件，可以看到QQ在向服务器回传不明数据，并且这些数据是经过加密处理过的。<br />
<br />
《科学时报》刊登的一篇文章也表明，腾讯附加在QQ程序中的浏览器程序，提供的一项叫做“谁与我同在”的功能，能让任何用户都可以查到当前浏览的网页上有哪些其他的腾讯QQ用户，同时，也可以监控用户正在察看哪些页面，这些搜集来的用户浏览资料，是可以被商业化利用的。<br />
<br />
就在记者开始着手调查并要求对腾讯进行采访的当晚，徐小姐的QQ号码竟然自动解封，可以使用了。<br />
<br />
记者致电腾讯公司市场部，并通过电子邮件发送采访提纲要求采访。记者的主要问题是：<br />
<br />
1，QQ是否在监视每个用户的使用情况？包括访问那些网站、下载了什么文档？<br />
<br />
2，QQ在多大范围内监视这些信息，监视哪些信息？<br />
<br />
3，作为一家民营的商业性质的公司，用户的个人资讯和使用习惯如何保证不被他用？<br />
<br />
4，对于QQ的监视功能，如果被黑客或者其他情报机构利用，腾讯该负什么责任？<br />
<br />
5，对于政府部门或者商业公司的机密，腾讯是否也可以获知？<br />
<br />
(三)理由――企图染上政治色彩 　　<br />
<br />
记者的采访提纲发出后，腾讯方面并未给予任何书面的文字答复，也没有做出正式的采访安排，而是通过北京的一家公关公司与记者联系沟通。<br />
<br />
该公关公司的人员按照记者留下的号码拨通了记者的电话，试图说服记者不要就此事进行追查和报道，她告诉记者，腾讯目前正处于强劲的上升趋势，而市面上各种即使通讯软件也是层出不穷，竞争十分激烈，如果因为记者的报道，引起腾讯QQ在商业上的损失，这是腾讯方面所不希望的。<br />
<br />
对于徐小姐QQ号码被封一事，该公关公司人士透露，是腾讯方面接到了上级有关部门的指令，才监视该用户的使用情况并封掉了她的号码，她不肯透露是是哪个部门要求腾讯这么做的，只是告诫记者，如果就此事进行报道，有可能会“牺牲”。显然是试图把商业问题蒙上一层政治色彩。而在听到这一消息后，徐小姐表示震惊和愤怒：“我怎么觉得阴森森的？把我当成国家的敌人了？”徐小姐还信誓旦旦地对记者保证，她绝没有通过QQ发布过任何不良信息，“如果有，让他们拿出证据来，发给谁了？”<br />
<br />
据记者调查了解到，对于网络公司及通讯软件公司，上级主管部门的确是提出过要求，对网络上有害的不良信息进行技术过滤和屏蔽，不得散布和传播，但却没有哪家公司接到过要求监视用户使用情况的指令，“真是要监视一个人，哪用得着他们呀”，一位曾作过情报工作的朋友这样告诉记者。　　<br />
(四)警惕――通讯软件安全有隐患<br />
<br />
一家通讯软件公司的技术人员告诉记者，在即时通讯软件中加进监控程序，在技术上不难实现，只要在用户本地机上加入几个关键词检索和过滤，就可以把关键信息传回服务器，而不用监视所有的聊天记录。<br />
<br />
有关人士介绍说，即时通讯软件都存在着安全隐患，作为消费者是有权知道这些的，而作为一家商业公司却没有权利监视跟踪用户操作记录的，尊重和保护用户的隐私及安全是国际上的通行商业准则。<br />
<br />
据赛迪网报道，6月14日，美国纽约州首席检察官办公室表示，AOL时代华纳旗下的Netscape将支付10万美元和解金，该公司因使用追踪用户下载情况的软件而遭投诉。<br />
<br />
另一方面，由于QQ本身的安全性能缺陷，针对QQ的各种黑客软件也在不断增长，在中国软件史上，QQ应该是受各种攻击最多的在线即时通讯软件。不少商业公司已经意识到QQ的安全问题，北京的一些单位和商业公司里，是严禁使用QQ软件的。<br />
<br />
在记者发稿前，腾讯方面通过电子邮件，给编辑发来了书面答复意见，而杭州的徐小姐却表示不会善罢甘休，她要“打电话问问看”。但无论如何，腾讯记录了该用户的使用信息，这不能不引起更多的使用者对网络通讯软件的安全问题引起重视。　　<br />
