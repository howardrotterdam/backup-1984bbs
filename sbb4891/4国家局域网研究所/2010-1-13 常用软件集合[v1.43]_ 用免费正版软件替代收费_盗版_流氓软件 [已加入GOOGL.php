<strong>[1]&quot;装机必备&quot;的神话. 自检: 你是不是&quot;装机高手&quot;?</strong><br />
<br />
现在许多初次接触电脑的人, 肯定是躲不掉那些&quot;装机必备&quot;的软件. 典型的此类软件有WinRAR, 迅雷, QQ及腾讯系列, windows优化大师, 360安全系列, 还有暴风影音, 等等等等. 各位可以自己检验一下自己电脑里是否存在这些东西. 这些软件是如何成为&quot;装机必备&quot;的呢?这个问题就不细说了, 一两句也说不清楚. 有兴趣可以参见[可能吧]的一篇关于暴风影音的文章.<br />
<br />
总之就是不要轻信&quot;装机必备&quot;. 这些软件可以说都是互相捧出来的: 迅雷的软件推荐里有winrar, qq, 360; 优化大师的推荐里有迅雷, qq, winrar, 360; 360的软件推荐里也有迅雷, 优化大师, qq, 暴风影音.... 这是一个无限的循环. 只要你拥有其中一个, 那你就很容易被推荐到他们其余的成员, 对于没经验的用户, 上手碰到的第一批软件总是容易信任的, 所以其&quot;推荐&quot;也就一起搭了顺风车. 更不要说各大软件下载站榜单前几名肯定少不了他们. 网吧里的预装系统肯定也有这些. 由此产生如下循环:<br />
-------------------------------------------------<br />
初次接触的新手:<br />
新手+网吧=&gt;接触使用[装机必备]--&gt;<br />
新手+个人电脑+下载站=&gt;下载[装机必备]并使用--&gt;<br />
或: 新手+ &quot;高手推荐&quot;/帮忙=&gt;安装[装机必备]<br />
新手+[装机必备]=&gt;下载被推荐的其他[装机必备]<br />
<br />
接下来, 如果<br />
网吧无[装机必备]=&gt;新手要求安装<br />
