<br />
<strong>[2]原则: 尽量到官方网站下载软件</strong><br />
<br />
不要随便搜索然后就找到一个不知名的下载站地址去下载. 首先下载站的广告十分具有欺骗性, 在真正的下载链接周围有各种醒目的&quot;立即下载&quot;&quot;高速下载&quot;, 让人防不胜防, 一不小心就被骗到广告地址, 甚至直接下载到病毒或者流氓广告软件, 安装了自己还不知道. <br />
<br />
<strong>[3]选择适合自己的软件</strong><br />
<br />
&quot;装机必备&quot;并不是对所有人都&quot;必备&quot;. 如果你只是偶尔上上网打打游戏, 那你可能根本用不上优化大师, 或者你根本不知道暴风影音的&quot;暴风盒子&quot;, 浏览器上的&quot;百度工具条&quot;是干什么用的. 那些你根本用不上的东西可能就是拖慢你电脑速度的原因. <font size="3"><strong>本文的目的, 就是针对各种&quot;装机必备&quot;, 提出各种替换的选择, 更多是为了提醒各位, 还有许多比&quot;装机必备&quot;更好用的东西, 每个人都可以有自己的&quot;装机必备&quot;</strong></font>. 正所谓&quot;授人以鱼不如授人以渔&quot;, 有自己的方法才是最重要的. <br />
