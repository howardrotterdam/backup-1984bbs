<br />
下面转个贴<br />
以下转自opm-server-mirror的google code项目说明：<br />
＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝<br />
<br />
DeployJavaOpmGuide&nbsp; &nbsp;<br />
Java版的Opm代理服务端架设指南<br />
在GAE上架设<br />
<br />
注意：在某些地区或运营商可能已经无法访问GAE了，电脑能访问手机未必能访问，反之亦然，自己看着办。<br />
准备工作<br />
<br />
过程并不复杂，你需要三个软件和一个gmail帐号<br />
<br />
&nbsp; &nbsp; * Java虚拟机，Windows用户可到Java官方网站下载并安装，Linux用户可搜索发行版的软件仓库。<br />
&nbsp; &nbsp; * GAE 的开发包，可到Google Code的官方网站下载zip文件。<br />
&nbsp; &nbsp; * Java版的Opm代理服务端，可到opm-server-mirror项目的下载页获取。<br />
&nbsp; &nbsp; * 一个GAE application id，如果你没有，可到App Engine首页免费注册一个，记下这个id名称。<br />
<br />
上传程序<br />
<br />
准备好上述资源后，按下面顺序操作。注意：如果你的GAE application上传过Python程序，上传Java版会冲掉原先的程序，你可以再注册一个新的来上传。<br />
<br />
&nbsp; &nbsp;1. 解压GAE 的开发包和Java版的Opm代理服务端，并把后者的文件夹（如“Opm_java”复制到前者的目录下，即“appengine-java-sdk-1.2.6/Opm_java/”<br />
&nbsp; &nbsp;2. 用你喜爱的文本编辑器（记事本也行）修改“Opm_java/war/WEB-INF/appengine-web.xml”文件，找到一行<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&lt;application&gt;application-id&lt;/application&gt;<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; 把“application-id”改成你注册到的GAE application id名称，修改好后保存。<br />
<br />
&nbsp; &nbsp;3. 接下来是命令操作了<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; * Linux用户，打开终端，进入GAE 的开发包的目录，运行下面代码<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;./bin/appcfg.sh update Opm_java/war<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; * Windows则用户，打开“命令提示符”，开始 -&gt; 运行 -&gt;输入cmd回车，进入GAE 的开发包的目录，如<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;C:\Documents and Settings\your_name&gt; D:<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;D:\&gt; cd D:\appengine-java-sdk-1.2.6<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;D:\appengine-java-sdk-1.2.6&gt; bin\appcfg.cmd update Opm_java\war<br />
<br />
&nbsp; &nbsp;4. 脚本会提示输入你的Email地址，输入你的gmail地址，然后会提示输入密码。注意：输入密码时不会显示星号的，直接输入去就是。<br />
&nbsp; &nbsp;5. 等待上传完成，很快的，才几十K的文件，当出现下面一行表示成功了。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;Update completed successfully.<br />
<br />
&nbsp; &nbsp;6. 上传好后测试一下，浏览<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;<a href="http://application-id.appspot.com/opm" target="_blank">http://application-id.appspot.com/opm</a><br />
&nbsp; &nbsp;&nbsp; &nbsp;<a href="http://application-id.appspot.com/ucweb" target="_blank">http://application-id.appspot.com/ucweb</a><br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; 如果跳到Google首页，表示成功了。<br />
<br />
&nbsp; &nbsp;7. 接下来修改你的Opera Mini或UCWeb里面的服务器地址为上面的地址吧，不同版本的教程可以从Wiki页获得。<br />
&nbsp; &nbsp;8. 最后就进行你喜爱的体育运动。<br />
<br />
在其它Java EE容器上架设<br />
<br />
&nbsp; &nbsp; 目前只测试过Tomcat<br />
<br />
&nbsp; &nbsp; * Tomcat，把“Opm_java”里的“war”文件夹改个名字如“Opm”，然后扔到Tomcat的“webapps”目录下，重启Tomcat，浏览“Opm/opm”就行了。<br />
<br />
高级设定<br />
修改代理网页的地址<br />
<br />
如果你不想用“opm”名称作为地址，想改成其它名称，如“opm.jsp”之类，可以修改“Opm_java/war/WEB-INF/web.xml”文件，找到下面一行<br />
<br />
&lt;url-pattern&gt;/opm&lt;/url-pattern&gt;<br />
<br />
把“opm”改成你想要的名称，如“opm.jsp”，修改后保存，然后重新上传。<br />
编译源代码<br />
<br />
源代码文件放在“src”目录，里面就一个“OpmServlet.java”和“geronimo-servlet_2.5_spec-1.2.jar”文件，修改源代码后，可用下面命令来编译<br />
<br />
javac -d classpath geronimo-servlet_2.5_spec-1.2.jar -d ../war/WEB-INF/classes/ OpmServlet.java<br />
<br />
编译和好后也需要重新上传。</div>

							
							
							
							
