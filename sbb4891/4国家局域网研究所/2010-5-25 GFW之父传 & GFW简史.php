<strong>GFW的前世今生，一部GFW之父方滨兴的发家史</strong><br />
<br />
<br />
标题的GFW之所以加上引号是因为，GFW是局外人起的绰号，它的真实称呼并非如此，但&quot;GFW&quot;也确实如实涵盖了这一在中国一贯隐晦而模糊的概念。<br />
 <br />
时间表<br />
<br />
- 1998年9月22日，公安部部长办公会议通过研究，决定在全国公安机关开展全国公安工作信息化工程DD&quot;金盾工程&quot;建设。<br />
 <br />
- 1999年4月20日，公安部向国家计委送交金盾工程立项报告和金盾工程项目建议书。<br />
 <br />
- 1999年4月25日，上万名法轮功练习者围攻中南海。<br />
<br />
- 1999年6月，国家计算机网络与信息安全管理中心成立，局级事业单位。<br />
 <br />
- 1999年7月22日，中华人民共和国政府宣布法轮功妨碍国家安全和社会稳定，认定法轮大法研究会及法轮功为非法组织，决定予以取缔。<br />
 <br />
- 1999-2000年，在哈尔滨工业大学任教多年的方滨兴调任国家计算机网络与信息安全管理中心副总工程师。<br />
<br />
- 1999年12月23日，国务院发文成立国家信息化工作领导小组，国务院副总理吴邦国任组长。其第一下属机构计算机网络与信息安全管理工作办公室设在已经成立的国家计算机网络与信息安全管理中心，取代计算机网络与信息安全管理部际协调小组，对&quot;公安部、安全部、保密局、商用密码管理办公室以及信息产业部&quot; 等部门的网络安全管理进行组织协调。<br />
<br />
- 2000-2002年，方滨兴在国家计算机网络与信息安全管理中心任总工程师、副主任、教授级高级工程师。<br />
<br />
- 2000年4月20日，公安部成立金盾工程领导小组及办公室。<br />
 <br />
- 2000年5月，005工程开始实施。<br />
<br />
- 2000年8月19日，大纪元时报创刊。<br />
<br />
- 2000年10月，信息产业部组建计算机网络应急处理协调中心。<br />
<br />
- 2000年12月28日，第九届全国人民代表大会常务委员会第十九次会议通过《关于维护互联网安全的决定》。<br />
<br />
- 2001年，方滨兴&quot;计算机病毒及其预防技术&quot;获国防科学技术三等奖，排名第一。<br />
<br />
- 2001年，方滨兴获国务院政府特殊津贴、信息产业部&quot;在信息产业部重点工程中出突出贡献特等奖先进个人&quot;称号，中组部、中宣部、中央政法委、公安部、民部、人事部等联合授予&quot;先进个人&quot;称号。<br />
<br />
- 2001年1月19日，国家计算机网络与信息安全管理中心上海分中心成立，位于上海市黄浦区中山南路508号6楼。国家计算机网络应急技术处理协调中心上海分中心是工业和信息化部直属的中央财政全额拨款事业单位。<br />
 <br />
- 2001年4月25日，&quot;金盾工程&quot;经国务院批准立项。<br />
<br />
- 2001年7月，计算机网络与信息安全管理工作办公室批准哈尔滨工业大学建立国家计算机信息内容安全重点实验室，胡铭曾、方滨兴牵头。<br />
<br />
- 2001年7月24日，国家计算机网络与信息安全管理中心广州分中心成立，位于广州市越秀区建中路2、4号。<br />
<br />
- 2001年8月8日，国家计算机网络与信息安全管理中心组建国家计算机网络应急处理协调中心，缩写CNCERT/CC。<br />
 <br />
- 2001年8月23日，国家信息化领导小组重新组建，中央政治局常委、国务院总理朱F基任组长。<br />
 <br />
- 2001年11月28日，国家计算机网络与信息安全管理中心上海互联网交换中心成立。提供&quot;互联网交换服务，互联网骨干网华东地区数据交换，数据流量监测与统计，网间通信质量监督，交换中心设备维护与运行，网间互联费用计算，网间互联争议协调&quot;，位于上海市黄浦区中山南路508号。<br />
 <br />
- 2001年11月28日，国家计算机网络与信息安全管理中心广州互联网交换中心成立，位于广州市越秀区建中路204号。<br />
 <br />
- 2001年12月，在北京的国家计算机网络与信息安全管理中心综合楼开始兴建。<br />
 <br />
- 2001年12月17日，国家计算机网络与信息安全管理中心湖北分中心成立。<br />
 <br />
- 2002年，方滨兴任中国科学院计算技术研究所客座研究员、博士生导师、信息安全首席科学家。2002-2006年，方滨兴在国家计算机网络与信息安全管理中心任主任、总工程师、教授级高级工程师，升迁后任其名誉主任。<br />
 <br />
- 2002年1月25日，报道称：&quot;国家计算机网络与信息安全管理中心上海互联网交换中心日前开通并投入试运行，中国电信、中国网通、中国联通、中国吉通等 4家国家级互联单位首批接入。中国移动互联网的接入正在进行之中，近期可望成为第五家接入单位。&quot;<br />
 <br />
- 2002年2月1日，国家计算机网络与信息安全管理中心新疆分中心成立。<br />
 <br />
- 2002年2月25日，国家计算机网络与信息安全管理中心贵州分中心成立。<br />
 <br />
- 2002年3月20日，多个国家计算机网络与信息安全管理中心省级分中心同时成立。<br />
 <br />
- 2002年9月3日，Google.com被封锁，主要手段为DNS劫持。<br />
<br />
- 2002年9月12日，Google.com封锁解除，之后网页快照等功能被封锁，手段为TCP会话阻断。<br />
 <br />
- 2002年11月，经费6600万的国家信息安全重大项目&quot;大范围宽带网络动态阻断系统&quot;（大范围宽带网络动态处置系统）项目获国防科学技术二等奖。云晓春排名第一，方滨兴排名第二。哈尔滨工业大学计算机网络与信息内容安全重点实验室李斌、清华大学计算机系网络技术研究所、清华大学网格计算研究部杨广文有参与。<br />
<br />
- 2003-2007年，方滨兴任信息产业部互联网应急处理协调办公室主任。<br />
 <br />
- 2003年1月31日，经费4.9亿的国家信息安全重大项目&quot;国家信息安全管理系统&quot;（005工程）获2002年度国家科技进步一等奖，方滨兴排名第一，胡铭曾排名第二，清华大学排名第三，哈尔滨工业大学排名第四，云晓春排名第四，北京大学排名第五，郑纬民排名第七，中国科学院计算技术研究所有参与。<br />
 <br />
- 2003年2月，在北京的国家计算机网络与信息安全管理中心综合楼工程竣工。<br />
 <br />
- 2003年7月，国家计算机网络应急处理协调中心更名为国家计算机网络应急技术处理协调中心。<br />
 <br />
- 2003年9月2日，全国&quot;金盾工程&quot;会议在北京召开，&quot;金盾工程&quot;全面启动。<br />
<br />
- 2004年，国家信息安全重大项目&quot;大规模网络特定信息获取系统&quot;，经费7000万，获国家科技进步二等奖。<br />
 <br />
- 2005年，方滨兴任国防科学技术大学兼职教授、特聘教授、博士生导师。<br />
<br />
