2. 墙在url上另外一个设计局限是，显然墙把整个url path(query string以前的部分）作为了封锁的键，而不是把整个域或IP地址（ip同样有问题，因为可能对应很多ip地址）。带来的问题是，对于一些通配域名（大多数blog提供商都采用通配域名），这有几乎是无限多的组合，那么一个站点就可以在墙的禁止表中产生无限多的记录，这为DDoS攻击提供了非常好的温床，一个简简单单的javascript代码就能成为墙的噩梦。<br />
<br />
例如， sohu的blog服务是采用通配域名的，我们可以随意输入任何域名: 1.blog.sohu.com, 2.blog.sohu.com, ... 都将解析成为一个有效的主机，实验表明，对1.blog.sohu.com采用一个含违禁词的query string, 导致1.blog.sohu.com被禁，而2.blog.sohu.com仍然有效。<br />
<br />
基于此原理采用&lt;img src=&quot;http://site/?pattern&quot; /&gt;就可以产生相同的效果。这就是说一个几行字的javascript可以轻松在不影响用户浏览，甚至用户不知情的情况下在墙上贡献n多个记录。如果这样的代码被放在有众多受众的web上，那么墙可能面临前所未有的冲击。&nbsp;&nbsp;墙是否能因此而崩溃是不得而知的，因为对付这种类型的DDos并非困难。处于众所周知的原因，测试代码不在这里公布了，但稍有javascript知识的朋友就知道这太容易了。<br />
<br />
――――――――――――――以上内容作废―――――――――――――<br />
