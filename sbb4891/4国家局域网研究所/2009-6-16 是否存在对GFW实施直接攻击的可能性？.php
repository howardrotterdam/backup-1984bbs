Updated: 墙还是比想象要聪明一些:<br />
<br />
- 试验表明当同一个域名下的url撞墙次数过多后，所有此域名下的url都会撞墙，因此上述简单地用通配域名应该是不行的。<br />
<br />
看来很可能还是通过IP地址来进行封锁的而并非url. 刚才手工试验的时候只是尝试了1，2，a,b几个子域名，而*.blog.sohu.com有多个IP地址轮流服务的，因此刚才试验很可能是恰好轮流到一个没有被封的ip了。<br />
<br />
- 对这种情况下url撞墙封锁的时间貌似很短，只有1分钟到几十秒<br />
<br />
再次Update:<br />
<br />
考虑了一下，既然墙封锁的是ip而不是url中的host, 也不需要那么麻烦，采用javascript随机（或者在一定范围内随机）产生IP地址的办法是很简单的. 这样的副作用是，也许不小心就把你需要的IP也给墙了，在网页上放这样的代码可能造成用户体验的不佳。对于产生的无效地址是无所谓的，无效的地址和被墙掉的地址区别不大。<br />
<br />
我的实验代码采用setTimeout() 和 &lt;img src= /&gt;,&nbsp;&nbsp;每500ms更换一个新的地址，根本不关心request发出去后怎么样，试验表明无效地址对浏览器端不构成什么影响。<br />
