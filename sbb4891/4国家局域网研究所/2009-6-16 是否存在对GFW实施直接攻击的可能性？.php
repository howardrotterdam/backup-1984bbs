																											<div id="postmessage_127520" class="t_msgfont">关于墙的技术讨论<br />
<br />
看到一篇关于墙的技术讨论，作者似乎是在Redmond的同仁：<br />
<br />
――――――――――――――――――――――――――――――――――<br />
<br />
&nbsp; &nbsp; 看来最近射击墙的理论已经几乎要进入实战了.<br />
<br />
&nbsp; &nbsp; 1. 今天周曙光在博客上写了对53端口的UDP 攻击可以实现. 相信不少人看到了.<br />
<br />
&nbsp; &nbsp; 2. 几星期前, 译言的一篇关于墙的工作原理的论文得到翻译, 也讲了对墙的 DDoS 方法<br />
<br />
&nbsp; &nbsp; 3. 一个多月前, 我在一个讨论组也发了类似的信, 可以借助在国外访问国内网址并且加入穿墙词攻击墙.<br />
<br />
&nbsp; &nbsp; 看来, 对于墙发动分布式攻击的时机和理论慢慢成熟了,&nbsp;&nbsp;但是现在没有人去做这个事情. 在此, 我提几个大纲性质的东西, 供大家参考:<br />
<br />
&nbsp; &nbsp; 1. 墙的计算能力是不可测的. 墙后面会异步的挂机群, 随时计算. 因此, 大计算量暂时打不倒墙. 墙也不会才去OCR搞图片或者反解简单加密的内容.<br />
<br />
&nbsp; &nbsp; 2. 墙的存储能力是极其有限的. 为了保证包匹配的快速, 墙必然在路由层面建立了匹配表. 匹配表的快速查找的需求决定了墙不可能有很大的存储空间(O(logN) 甚至 O(N) 查找时间). 而墙的设计漏洞在于, 一个记录必须在存储器里面保留5分钟.&nbsp;&nbsp;因此, 只要发动分布式射击, 墙必然会被包头塞满. 只要做到这一步, 墙就等于无用了.<br />
<br />
&nbsp; &nbsp; 3. 墙是点对点的. 所以, 单点射击不影响其他人使用. 在国内对国外放散弹或者在国外往国内放散弹都可以塞满墙的查找表.<br />
<br />
&nbsp; &nbsp; 4. HTTP 协议也可以用来打墙. 嵌在 IFrame 中的连接请求加上一个精心设计的 URL参数, 即可在墙上留一个枪眼五分钟. 无需任何资源. 无需对方配合. 这个为开发分布式提供了极大便利. 因为浏览器就是武器.<br />
<br />
&nbsp; &nbsp; 同时, 鉴于没技术人员发起, 我暂时充当一下发起者. 有兴趣的开源开发者请踊跃参加, 争取拿金牌. 了解网络协议者优先, 会写 widget 或者 SNS App者优先. 有意者留言. 或许下一届奥运会射击金牌, 就是你的! 我真心的希望, 我们这一代人, 经过几年努力, 能拿到这个金牌. 毛主席说, 既要能绕开, 也要能主动攻击, 兵法即如此.<br />
<br />
――――――――――――――――――――――――――――――<br />
<br />
但是所谓道高一尺魔高一丈，作为扩展的国家机器的墙的威力（计算能力）还是不容忽视的。<br />
<br />
yiyan的这篇文章<a href="http://www.yeeyan.com/articles/view/40085/11992" target="_blank">http://www.yeeyan.com/articles/view/40085/11992</a>非常不错，很值得学习。在这篇文章的基础上做了几个简单的实验，发现目前的墙的算法还是很愚蠢的：<br />
<br />
1. 墙把url中的违禁pattern作为判断的重要依据，这样的好处是比较轻量级，比判断传送的网页的内容是否含有违禁pattern要轻量太多了，但带来的问题就是非常容易欺骗。例如<a href="http://www.sohu.com" target="_blank">www.sohu.com</a>是什么会的官方站点，当然是根红苗正的，但是我可以在其访问的url上任意添加query string, e.g. <a href="http://www.sohu.com/?falun" target="_blank">http://www.sohu.com/?falun</a>, 于是墙傻乎乎上当了，sohu在我这里就屏蔽了数分钟。 由于我在墙外，所以我拿墙内的网站试验，在墙内的可以拿墙外的试验，例如可以尝试： <a href="http://whitehouse.gov/?falun" target="_blank">http://whitehouse.gov/?falun</a> , 看看，白宫立刻被墙挥刀斩于马下。<br />
