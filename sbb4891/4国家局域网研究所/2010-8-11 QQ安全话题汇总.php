<br />
4.如需重启才能彻底删除某些文件,重启之后进入C盘，删除所有文件夹里写明TENCENT的文件夹和其下的所有文件。重点集中在小流氓的安装路径和c:users用户名appdatalocal 和roaming这两个文件夹下。 <br />
<br />
