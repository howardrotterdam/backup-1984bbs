<br />
之前网上已经有很多帖子描述过QQ这个小流氓如何擅自偷偷在系统底层扫描并上传用户数据的行为。这里不多重复。现在说一下更恶劣的。注：我的系统是VISTA的，微软系统应该都差不多。 <br />
<br />
只要你把QQ登陆之后，打开你的系统资源监视器（资源监视器位置：任务管理器-性能-资源监视器）中的网络状态栏。会看到好几个QQ.EXE程序在忙活。此时你还根本没有跟任何人对话，它自己在不停扫描你硬盘的文件同时在发送可疑数据。 <br />
<br />
OK。现在你把QQ连接断开，同时断开你的网络连接。注意，我这时甚至把电脑的无线网络开关都已经关了。继续看这个网络栏。这个小流氓QQ程序居然还在忙活，居然可以绕开你的电脑本身连接直接通过网关在发送数据包。够流氓不？够危险不？整个显示的运行程序就是QQ.EXE。这个小流氓不开源，没人知道它怎么做到这一点的。 <br />
