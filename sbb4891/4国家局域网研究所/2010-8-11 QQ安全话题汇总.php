<br />
1.把你电脑防火墙里允许通过网络的小流氓程序给删除。如果你用了防火墙的话。如果没有防火墙，这一步可以忽略。 <br />
<br />
2.必须安装微软官方版的软件清理实用工具--Windows Install Clean Up该工具中文详细官方说明地址：<a href="http://support.microsoft.com/kb/290301/zh-cn" target="_blank">http://support.microsoft.com/kb/290301/zh-cn</a> 这是彻底根除赖在你系统配置里的软件必须要做的第一步。安装运行这个程序之后，你会看到你的清理列表里，除了微软本身的软件以外，赫然就有QQ和TOM SKYPE这两个所谓的通讯软件。正常的软件是不会出现在系统配置列表里的，可见其多么流氓无赖。 <br />
<br />
然后按照说明选择这个程序删除-确认-退出。至此，小流氓的系统配置数据彻底从你电脑里清除。包括它强行进驻到系统SYSTEM32文件下的配置都会被彻底清理掉。 <br />
<br />
