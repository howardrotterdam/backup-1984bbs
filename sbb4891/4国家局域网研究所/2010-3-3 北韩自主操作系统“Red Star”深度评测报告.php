新闻来源:华军资讯<br />
<br />
据国外资讯网站RT报道，朝鲜（North Korea）不仅拥有自主的、与基于美国的万维网相互独立的区域网络，而且还拥有自行研发的红星Red Star操作系统。据悉，红星OS是去年由朝鲜IT技术人员在Linux操作系统的基础上改进而来的。俄罗斯学者Mikhail利用他在金日成大学交流之际获得了这款红星 OS。他花费了5美元在距离宿舍五分钟步行路程的地方购得两张安装光盘。系统共分为客户端和服务器两种版本，第三方软件可以另外花钱购买。<br />
