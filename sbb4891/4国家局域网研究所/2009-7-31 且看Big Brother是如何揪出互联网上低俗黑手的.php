																											<div id="postmessage_195990" class="t_msgfont">呵呵。这样讲吧。<br />
<br />
今天我买了一本钱总的《想象中的动物》，里面是不太相关的很多个故事。我把这本书，和《大英百科全书》一起每句话都用剪刀剪开，然后重新打乱了粘在一起。我仍然可以读，因为我记录好了一个查询的清单，知道哪一句话后面接的下一句在哪里。<br />
<br />
油墨是印在纸上的，所以我们看得到。就如同磁粉在玻璃片上一样。每个磁粉的不同的指向，决定了这个比特是0还是1，之后才有了磁道、扇区、以及硬盘。磁粉指向的物理性质被改变，不管他是金正日，还是伟光正，都没有办法。硬盘内部是密封的，保证磁头，和磁盘表面磁粉的读取，不受干扰。你打开硬盘的密封，如果不是100%的净室，灰尘进去，磁粉的物理性质会被破坏，磁头也会被 干扰。你摔碎硬盘，盘片的大面积磁粉，当时就消磁了，不仅仅是断裂部分。<br />
<br />
所以，我把那本打乱了顺序的《想象中的动物》+《大英百科全书》，扔到水里泡了两天，然后再拆散剪开。最主要的是，那个寻址的查询清单已经没有意义了。<br />
<br />
我们不去考虑这样的残片如何用磁头去读出，磁头的定位是用微米为单位计算的；就假设你都能读出。拿着这样一堆残缺了大部分，而且顺序完全打乱的纸片，即使是可以读出只言片语，大部分还是《大英百科全书》。你想从其中恢复钱列宪老师的任何一个故事，哪怕仅仅是一段话，你觉得可能性多大？<br />
