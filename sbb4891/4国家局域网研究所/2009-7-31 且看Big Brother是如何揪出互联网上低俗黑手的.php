根据上述查询结果，我们找到安吉时空在线网吧，并在该网吧机房内找到“雅轩阁”网站服务器和存放网站程序的电脑硬盘。在查处 “久爱音乐网”（<a href="http://www.9imc.com" target="_blank">www.9imc.com</a>）时也同样用了此方法在德清风尚网吧净网先锋服务器上找到了“久爱音乐网”网站程序和歌曲曲目库。因中国互联网络信息中心IP地址数据来源APNIC（注 3: APNIC即亚洲太平洋地区互联网络信息中心，是现在在世界中操作的五个地区的英特网登记处之一,分配B类IP地址的国际组织。它提供全球性的支持互联网操作的分派和注册服务）,此方法可比较精确的查得网站IP对应使用者登记信息，但由于目前我国大多数上网用户IP地址不固定且未实名登记，通信（网络）运营商存在服务器租赁、托管、层层转包等情况，通过中国互联网络信息<br />
<br />
中心IP地址能马上确定服务器所在地的是少数，多数还需通过通信部门协查来确定服务器最终所在地。<br />
<br />
（3）通信部门协查。作为通信主管部门，各省（市）通信管理局（电信局）拥有互联网网站注册、备案（审批）人等很多详细而精确的信息，能得到他们的鼎力相助，对我们查处互联网案件往往事半功倍。如根据文化执法部门已掌握的信息，通信部门积极提供网络服务器使用人等网站有关精确而详实的资料，将非常有利于互联网案件迅速、有效查处。但由于文化管理部门与通信管理部门无隶属关系，且县（市、区）级的通信管理部门一般就是电信公司，既是通信管理部门也是通信运营商，其与网站开办单位（人）还存在客户关系，出于部门利益，易出现消极应付文化部门等情况，执法中可以灵活应对。如当文化执法部门找到通信运营商，请其协助提供IP地址对应服务器使用人信息时（一般在IP地<br />
<br />
址注册信息单、服务器主机租赁、托管等合同可以看到），运营商以商业秘密、电信相关管理制度规定等理由不提供，我们可以退一步，向其要有当事人信息和IP地址使用（服务器租用、托管）协议（合同）的部分内容复印件，并加盖其公章，只要能证明当事人使用该IP和服务器事实即可；还有在对违规网站开办人租用或托管在运营商那边的服务器检查时，如遇到运营商以服务器硬盘内还存有其他合法网站等理由对执法人员暂扣服务器不肯配合时，也可退一步用移动硬盘复制服务器内违规网站全部内容，并用信封封存移动硬盘后请其在信封封口和服务器现场检查记录上签章确认；对于拒不配合的执法的，还可以通过上级文化管理部门出面、向该运营商上级管理部门协调和反馈。<br />
<br />
3.综合查询方法。在方法1、2遇到困难时，还可以运用综合查询的方法。如通过仔细查看网站每一个网页内容，搜寻网站开办人各种有关信息，如QQ 号、电话（手机）号码、网店、银行卡号、论坛、博客、空间等（如发现QQ号，可通过QQ聊天锁定对方IP地址，再通过IP查询来定位当事人所在地；通过网店，查询当事人寄货地址；通过域名注册查询网站，如用<a href="http://www.dirs.cn/whoisQuery.html" target="_blank">www.dirs.cn/whoisQuery.html</a>，可查询网站域名注册人姓名、地址、电话等信息。拘于篇幅，这里不一一枚举）。总之通过多管齐下，全面搜索、查询，最终根据查得的单项或者综合信息得已突破，来成功找到网站开办人及所在地等信息。<br />
<br />
（三）现场执法取证。<br />
<br />
根据上面检查、查询掌握的网站开办人、服务器所在地等信息，如网站开办人、服务器在同一地方，且为网吧、公司等公共场所，我们可以找当事人在的时间立即前去查处（如场所为私宅，应请公安部门协助在征得当事人同意再进入。对当事人居无定所或居住地频繁变动的也可请公安部门利用户籍、身份证信息、暂住登记等途径协查）。如网站开办人和网站服务器不在同一地方，在赴当事人所在地检查时，最好另派一组执法人员到网站服务器所在地，并同时进入现场。在这两个现场，两组执法人员应密切配合，分工合作，相互促进，推进案件突破。通过检查网站服务器、当事人用于维护违法网站的电脑，找出违规网站相关证据。并向网站开办人出示前期调查已掌握的证据，指出其违法违规行为，<br />
<br />
晓以利害，争取其主动配合调查取证，告诉网站管理后台、网站服务登陆密码，配合执法人员做好网站后台证据截屏、服务器主机或硬盘封存。这里要特别强调的是，在条件允许的情况下，一定要对服务器硬盘采取强制措施。因为作为互联网案件中唯一“物证”，网站服务器硬盘存有违规网站程序、网页、电影(歌曲) 曲目库、点击率等大量关键信息。一旦找到了此硬盘，当事人最后一道心理防线可以突破了，日后办案中补充证据有依据了，我们作出行政处罚心里有底了，面对行政复议（诉讼）不怕了。所以对此硬盘，有法律依据能收缴的一定要收缴，不能收缴可暂扣的一定要暂扣（或异地证据先行登记保存）；如因客观条件限制，收缴、暂扣或异地先行登记保存实在有困难，也应用移动硬盘复制<br />
<br />
硬盘内有关违规网站的全部内容，以备日后不时之需。封存硬盘时，一定要当着网站开办人的面，装入信封封好，并请其在信封封口处签名确认，防止其日后反驳说硬盘内数据已被修改过。日后因办案需要要查看硬盘内数据时，也要请当事人在场拆开进行，并全程摄像。<br />
<br />
封存硬盘后，还要填写现场检查笔录、暂扣（封存）通知书、责令改正（停止视听节目服务）通知书。并现场制作调查笔录，问清何人、何时、为何、如何开办（维护）了该网站，以及网站内曲目库数量、点击数、赢利数等信息，与前面取得的证据相互呼应，形成证据链。最后请当事人在相应文书（照片）上签字确认。<br />
<br />
三、几点经验和想法<br />
<br />
互联网案件查处难度大，查办经验少，上述案件的成功破获得益于以下几个方面：1.领导重视。发现上述违规网站线索后，湖州市文化广电新闻出版行政部门领导非常重视，严密部署查处方案，并抽调执法机构计算机、办案、法律相关精干力量组成专案组，负责案件查处工作，并积极联系相关部门协助调查工作，为成功查办案件创造良好的条件。2.部门协助。由于网络虚拟性和无地域性，违法网站服务器和当事人所在地往往不是同一地点，找到网站开办当事人和服务器所在地难度较大，需加大与公安、通信管理部门和兄弟省市文化执法部门的通力合作。目前在查办互联网案件中，公安协助积极性、配合度均较好，但地市通信管理部门（电信局）配合程度视与其部门利益关系时好时坏，下一步就如<br />
<br />
何在体制、机制、工作上与通信管理部门明确监管职责，加强相互协作和信息通报，及时发现违规网站、迅速合力查处，共同促进网络规范有序，是非常值得探索和改进的。3.行动周密。由于网络证据的不确定性，当事人一旦察觉，可在几分钟甚至几秒之内迅速销毁电子证据。因此，上述案件办理中，在赴当事人所在地前，执法人员都事先在办公室，通过对网站远程核查、截屏、拍照、刻盘等方法，固定网站违规的相关证据；在现场检查中，如当事人所在地与服务器所在地不在同一处，尽量派两组执法人员同时执法，一方面控制服务器存放现场，一方面检查当事人用于日常维护网站的电脑，并防止证据被销毁，充分固定网站违规相关证据。4.主动出击。随着互联网迅速发展和普及，其在给文化市<br />
<br />
场发展提供新平台的同时，也带来低俗有害信息、盗版侵权盛行等诸多问题，需加强主动监管。一是要不断完善净网先锋等网吧监管平台，充分发挥其网络监控过滤等功能；二是在检查中，要把网吧客户机内容作为日常检查重点内容之一；三是要加强对辖区内大型知名网站的监管。<br />
<br />
上述违规网站的发现除了部分是上级利用监控、搜索平台发现移交我市的，其余的均为执法人员在日常检查网吧客户端电脑和辖区论坛内容发现线索的。不过网（站）海茫茫，而文化执法部门人数、精力有限。因此有条件的地方应尽快配备网站监控搜索系统（平台），提高违法网站的发现速度和执法检查的针对性，有效查处文化市场综合执法中各类互联网违法违规案件。<br />
