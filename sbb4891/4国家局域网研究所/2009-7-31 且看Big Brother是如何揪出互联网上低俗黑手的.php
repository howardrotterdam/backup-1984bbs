													<h2>且看Big Brother是如何揪出互联网上低俗黑手的</h2>
						
						
																											<div id="postmessage_185503" class="t_msgfont">[征文]浅谈文化市场综合执法中互联网案件的执法取证<br />
<br />
湖州市文化市场行政执法支队&nbsp;&nbsp;施旭林<br />
<br />
自2009年1月份以来，湖州市文化市场行政执法机构根据上级统一部署，集中力量开展了整治互联网低俗之风专项行动，成功破获并责令关停了“KK雷区-迅雷电影”、“雅轩阁”、“易乐港博客”等一批违法违规网站。笔者有幸参与了上述案件的查处，在执法办案中结合自己所学计算机专业和已掌握的文化市场执法取证知识，就互联网新领域案件执法取证进行了探索尝试，并在执法实践中积累了点滴经验，现与大家一起分享探讨。<br />
<br />
一、互联网案件的特点和执法取证中的难点<br />
<br />
与传统文化市场案件相比，因网络的虚拟性、跨地域性、高新技术性等特性，涉互联网案件往往具有专业性强、手段隐蔽、跨地域广、证据易删除等特点，在执法实践中常会遇到以下难点：一是确定违规网站开办人难；二是找到违规网站开办人难；三是找到网站服务器最终存放地难;四是固定网站违法证据难。再加上目前文化执法部门缺少必要的互联网相关信息查询平台和技术设备，执法取证难度显而易见。<br />
<br />
二、互联网案件执法取证步骤和方法<br />
<br />
根据互联网案件上述特点，为克服、改善查处互联网案件面临的难点，互联网案件执法取证一般可按以下三个步骤进行：（一）办公室远程核查，固定违规网站相关证据。由于互联网案件中的证据都是以电子信息的形式存贮于电脑中，极易被修改删除销毁。因此在发现违规的网站后，应迅速组织两名以上执法人员在不惊动当事人的情况下，通过互联网远程访问违规网站，查看网站内容，下载网站内提供的电影、歌曲等违规内容，对下载的内容和网站相关页面进行截屏，刻录成光盘，填写网站核查记录，并对检查取证过程全程摄像。有条件的单位还可以邀请公证人员对检查取证过程进行公证，并出具公证书。<br />
<br />
（二）查询网站相关信息，确定网站服务器所在地和开办人信息。一般可以采用以下三种方法：<br />
<br />
1. 备案信息查询法。根据国家相关规定，开办网站需先备案，备案时需注册网站开办人相关信息，我们可按以下操作找到网站开办人信息：<br />
<br />
(1)找到网站备案号（一般标注在网站首页末尾，形如“浙ICP备******号”，浙代表浙江省简称，*代表数字。如浙江省文化厅网站（<a href="http://www.zjwh.gov.cn" target="_blank">www.zjwh.gov.cn</a>）备案号为浙ICP备05000050号）;(2)登陆工业信息化部ICP信息备案管理系统（<a href="http://www.miibeian.gov.cn/CX/main.jsp" target="_blank">http://www.miibeian.gov.cn/CX/main.jsp</a>）, 点击公共信息查询，选择备案/许可证号查询，输入网站备案号（或域名等其他已知的信息）,输入相应验证码，点击查询，即可查得该网站备案时提供的主办单位名称（主办人姓名）等信息。点击浏览，进一步查询还可以查得网站开办单位名称、负责人姓名等信息。此法适用已备案且如实备案的网站，便捷快速。通过工信部给省级管理部门（如浙江省通信管理局、浙江省广电局等）的密钥，还可以查询到网站开办人身份证号、电话、住址等具体信息，使我们可以迅速找到当事人。但由于目前通信管理部门对网站备案程序、要求、审查不是很严，网站开办人备案时并非都是实名、完全登记，存在备案信息部分或者全部为虚假的情况，尤其是那些以牟利为目的非法网站，往往用虚假信息备案登记来逃避管理部门查处，但对大多数非蓄意违规开办的网站来讲，不失为一种便捷高效的好方法。<br />
<br />
2. IP地址查询法。按查询方便和精确程度列举三种方法：<br />
<br />
（1）IP查询网站查询。如通过IP地址查询网--<a href="http://www.IP138.com" target="_blank">www.IP138.com</a>（注1：<a href="http://www.IP138.com" target="_blank">www.IP138.com</a>仅为众多提供公共查询的网站中笔者习惯使用的一个，具体读者可根据自己习惯选用相关网站进行查询，下同），输入网站域名即可查到网站对应IP地址及网站服务器大概对应地址。以“雅轩阁”网站为例，通过在IP地址查询网中输域名“<a href="http://www.001sk.cn" target="_blank">www.001sk.cn</a>”，点击查询，即可查得如下结果。<br />
