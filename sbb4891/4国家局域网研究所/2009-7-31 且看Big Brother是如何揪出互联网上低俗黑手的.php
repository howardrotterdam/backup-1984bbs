<br />
不过四一哥，那个军方人士纯属扯淡的，你让丫来试试读砸碎的硬盘的数据。。。。。。。磁头的定位需要非常的精确，尤其是现在硬盘都是多片的，砸碎了丫根本读不出来。而且砸碎了以后，断裂部分的磁道就损坏了，而且周围也就完全消磁了。而且运行过整理硬盘的就知道，同一个文件，硬盘存储并不一定是连续，所以引导区坏了之后，根本没有办法读出来。<br />
<br />
硬盘删除分区之后，之前数据还在。重新再分区之后，之前数据也还在。快速格式化之后，数据也还可以恢复，但是如果你重新分区，新的分区和原分区大小不同，或者物理的起始和终止磁道不同，就很难恢复，但也存在恢复的办法。正常格式化之后，新分区没有过多读写操作，数据依旧可以回复。<br />
<br />
但是只要低级格式化硬盘之后，再等一小会保证彻底消磁之后，谁也没有办法恢复。有很多低级格式化硬盘的工具。</div>

							
							
							
							
