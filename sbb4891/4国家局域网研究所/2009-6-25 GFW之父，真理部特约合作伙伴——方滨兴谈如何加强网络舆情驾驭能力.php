<br />
众所周知，北京邮电大学是新中国第一所邮电高等院校，被誉为“信息黄埔”，为中国电信事业的发展贡献了一批有一批的人才。可以说，中国电信业的发展是与北邮密不可分的。<br />
互联网大潮浩浩荡荡，在三网融合的时代，互联网将成为联接世界的主要工具。今天在北邮电院、计科院、信院的实验室内，无数的老师、学子正为建设更加安全、高速的网络而努力，可以说，国际电联提出的任何时间、任何地点、任何人之间的通信是广大北邮人的奋斗目标。在这里，我可以负责任的说，北邮没有任何实验室在做对不起中国网民的事情！<br />
<br />
继续说方滨兴校长。2005年，还在哈工大的方滨兴以44岁的年龄当选工程院院士，是因为他为国家信息安全做出了巨大的贡献。但是，维护国家信息安全怎能和GFW这种下作的产品等同起来呢？二者的区别，无异于拿google比绿坝。google一下，我们就可以看出，方滨兴的主要研究领域为：<br />
网络安全、信息内容安全、并行处理、互联网技术等，首先提出了建设国家网络与信息安全基础设施的理念，并组织研制、实现了相应的系统，作为第一完成人，先后获得国家科技进步一等奖1项、二等奖2项。在信息安全理论方面，将涉及了物理安全、运行安全（网络安全）、数据安全（狭义的信息安全）及内容安全的信息安全概念给出了统一的形式化定义，同时着手提出并研究信息安全属性可计算性的思想。<br />
再来看方校长的科研成果：<br />
1、2001年，“计算机病毒及其预防技术”获国防科学技术三等奖，排名第一；<br />
2、2002年，“信息安全管理系统”获国家科学技术进步一等奖，排名第一；<br />
3、2002年，“大范围宽带网络动态处置系统”获国防科学技术二等奖，排名第二；<br />
4、2004年，“大规模网络信息获取系统”获国家科学技术进步二等奖，排名第一；<br />
5、2004年，“国家信息安全展略研究”获国家发展改革委机关优秀成果三等奖；<br />
6、2005年，“搜索引擎安全管理系统”获中国通信学会科学技术二等奖，排名第二；<br />
7、2007年，“通信数据安全管理系统”获国家科学技术进步二等奖，排名第一。<br />
哪一条和GFW有五毛钱的关系？可见，那些攻击方校长为GFW之父的人纯属是无中生有，造谣诽谤。<br />
<br />
方校长为北邮的第七任校长，上任以来，对内积极收紧校财务，关心学生工作。在学十三着火、学五跳楼、食堂“豆制品门”等突发事件中沉着稳定，处理得当，北邮学生情绪稳定，校园温馨和谐。对外积极推动“学士后”“硕士后”学位的设置，为解决我国的大学生就业献计献策。这样的一个好校长，你们人肉他只能让他身上的光芒更加闪耀！<br />
<br />
因为方校长的以上种种突出贡献，2007年他同我校知名教师许志永等人同时当选人大代表。这本来是无上的光荣，但竟然有人污蔑方校长的某提案为绿坝的出台做了人大准备。方校长作为全国人大代表为我国的下一代苦心积虑，又身为信息安全专家，绿坝这种漏洞百出的流氓软件怎能和方校长挨上边？<br />
<br />
由于方校长到北邮的时间并不长，可能大部分的北邮同学并不知道方校长的研究领域和成果，总有一天，方校长会在合适的时候详细公布自己的研究成果，给世人一个榜样。同时，我相信方校长光明磊落，一定会公开自己所有的人大提案，彻底堵住那些无中生有的人的嘴！</div>

							
							
							
							
