													<h2>OSO：“拯救”硬盘！</h2>
						
						
																											<div id="postmessage_810832" class="t_msgfont">缘起：<br />
<br />
挪动笔记本的时候不小心使移动硬盘插头摇动，好像还是连续几下，硬盘接连断接了几次。当时是否正在读取或写入数据不详。<br />
<br />
症状：<br />
<br />
插入后，硬盘绿灯亮，持续“咔咔”响，而系统检测不出该盘。试用“PTDD分区表医生”、“Disk Genius”、“Norton PMagic NT”均检测不到该盘。<br />
<br />
诊断：<br />
<br />
先插入该硬盘，后重启系统，依旧。<br />
硬盘“咔咔”声很大，持续不断。慢慢改变硬盘的空间状态，垂直、旋转、翻转，在某个状态下“咔咔”声停止，绿灯变为红灯。期间系统响起过检测到外设的“”，但其实没有检测到，偶尔还出现连续的“……”声。<br />
<br />
判断：<br />
<br />
未能读取硬盘0磁道，磁头在不断移动寻道。<br />
既然所有系统工具都不能检测到该盘，应该是硬盘本身问题，而且估计是硬件问题，甚至机械问题。<br />
<br />
修复：<br />
<br />
曾有过供电不足引起硬盘寻道不止的现象。使用该硬盘连线的并联插头取电，并改变插入的先后顺序，均无效。<br />
换用另一个移动硬盘接口板，两个接口板对另一个硬盘均可用，但是对该硬盘：换用另一个接口板后毫无反应。<br />
考虑将该硬盘接到主板尝试，但接口不同，作罢。<br />
<br />
“拯救”：<br />
<br />
在想不出任何办法之后，加之顽劣的“好奇”本性，决定拆开硬盘。亦心存侥幸：或许机械问题可以拨弄一下，甚至打开装上之后就好了（这其实也是可以偶遇的）；简单说，就是“卡”住了。<br />
<br />
图1：<img src="http://i52.tinypic.com/24yagr4.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
1、硬盘封固螺栓是“内六星”型（不知道专业名称，自命的）。要注意的是：很多电气产品的封固螺栓会至少有一个隐藏在标签封盖之下！<br />
用吹风机吹热后揭开。松开所有螺栓后，用薄口螺丝刀小心沿四周撬起硬盘盖。<br />
图2：<img src="http://i54.tinypic.com/2j0imw3.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
2、这便是硬盘真面目。<br />
给我的第一启发是：如果硬盘彻底报销后，拆下盘片，可以做一面好镜子，而且可以有某种特殊用途。只是还不知道其介质是什么材质，若是玻璃就要小心了。可以看出：现在磁头处在正常位置。小心拨动磁头臂和一些联动部件（左上角），未见异常。<br />
图3：<img src="http://i54.tinypic.com/5umxw9.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
插入电脑，观察其运行：磁头进到磁盘大约一半半径位置既返回，返回原位时撞击声音很大，就是那个“咔咔”声。改变硬盘空间状态，红灯亮时磁头停在磁盘大约一半半径位置不动。<br />
图4：<img src="http://i51.tinypic.com/2u5gfmo.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
3、为将此状态存照，用了比较长时间，拍了若干张。因为光线问题，采用遮挡、强闪等方法。结果很悲惨，造成彻底损坏：由于磁头不动，划伤磁盘盘面。<br />
图5：<img src="http://i54.tinypic.com/rkmnx5.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
4、看到磁头只进到磁盘一半位置，马上想：是不是没有进到0磁道？为此反复寻找是不是磁头臂运动受卡。后来查证：0磁道应该是最外磁道。不知是在此过程还是由于前面磁头长期停滞不动，后来磁头不能进入磁盘盘面区域，只能在保持初始位置的那个白塑料架内做小范围移动。仔细检查，发现：磁头臂最前端大约1mm宽2mm长的针状小臂进错了白塑料架轨道而被限。只好拆下白塑料架使其归位，但发现针状小臂已经向下弯曲。小心使其恢复后，能够如前正常移动。当然，原故障如故。<br />
<br />
困惑：<br />
<br />
一般都说硬盘磁头是悬浮的，与盘面没有物理接触。那么：<br />
为什么磁头停滞不动时，会造成磁盘盘面划伤？<br />
针状小臂到底是什么时候弯曲的？是我的不当操作引起的，还是造成整个问题的初始原因；也就是说当硬盘读取或写入数据时突然断电，引起磁头臂掉电回位造成的？<br />
既然硬盘磁头与磁盘盘面是悬浮而没有物理接触，为什么突然断电会引起磁盘损坏？还是只会引起数据丢失？<br />
注：白塑料架仅仅是个轨道限制，没有任何电信号控制；就是说，是个死家伙。没有观察到磁头臂是否有轴向的移动，似乎没有。那么，不存在磁头臂归位时的选择轨道入位问题。<br />
<br />
下场：<br />
<br />
好奇心满足了，复位。怀着窘迫小人的侥幸之心，再次重复前面的几个方法。当然，一切如故。<br />
暂时还没有拆下磁盘镜面。一是还没有想出做什么用；二是拆下的其他部件散开后难以保存；三是看着完整的尸体，还心怀留恋：这是我第一个移动硬盘，而且是一直保存我收藏的所有资料的移动硬盘。<br />
<br />
既不是求救，也不是经验介绍，只是满足同有此好奇心的朋友的好奇心：）<br />
<br />
提醒大家的是：不要小看外设的非法插拔！<br />
<br />
几个困惑，还望高手指教。谢谢！</div>

							
							
							
							
