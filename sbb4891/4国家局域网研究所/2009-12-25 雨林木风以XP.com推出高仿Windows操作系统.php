<br />
本人是支持的，不管加了多少垃圾甚至是没做什么大的修改，只要能加大LINUX的用户基础，总是好的。<br />
再说这个系统主要面向Linux小白的，那些号称蔑视这个系统带的大虾实在弱智的很。就像DND众老妖一样的德行。<br />
<br />
说实在的做技术的，懂商业的很少，YLMF还是不错的。<br />
<br />
<img src="http://img3.cache.netease.com/tech/2009/12/24/2009122423263835977.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
<br />
网易科技讯 12月24日晚间消息，雨林木风工作室在宣布解散一年之后，雨林木风官方团队在平安夜发布了“Ylmf OS”开源操作系统。引人关注的是，雨林木风操作系统界面与Windows XP非常类似。此外，雨林木风官方证实此前耗资10万美元购得的XP.com域名，有可能会用于该开源操作系统的发行宣传。截至发稿时止，XP.com域名已经指向雨林木风操作系统的发布页面<a href="http://www.ylmf.org" target="_blank">www.ylmf.org</a>。<br />
<br />
对于选在工作室解散一周年之际发布操作系统，雨林木风官方公开表示，“Ylmf OS”是雨林木风工作室解散一周年特别纪念版，意在表明继承和转型的核心理念，并增进产品与用户的互动。公告称，“那段岁月虽一去不再复返，但雨林木风人将秉承原工作室的拼搏与奋斗精神，继续努力为民族软件产业的振兴添砖加瓦！相信经过大家的努力，Linux开源系统也可以做得和Windows那样出色，给广大用户多一个选择。”<br />
<br />
官方介绍称，“Ylmf OS”不仅系统界面默认为精仿的 XP 主题，系统还集成OpenOffice办公套装、浏览器Firefox、QQ即时通讯、Linux下的在线电影平台等大量能满足国内用户日常使用的必备软件，用户在Windows平台下的基本应用都可以在这个操作系统上良好运行，同样具有很好的用户体验。“到目前为止，Ylmf OS是最接近Windows XP的开源操作系统，连操作习惯都没什么区别。”雨林木风操作系统的研发人员称。<br />
<br />
除了精仿XP主题的系统界面外，雨林木风今年9月份通过易名中国巨资收购的XP.com域名的具体用途再次引起业界的猜测。雨林木风官方证实，XP.com将会考虑用于开源操作系统的发布宣传。或许微软没有想到，雨林木风工作室在解散一年后，雨林木风官方重新回归操作系统并正式向Linux开源敞开了怀抱。<br />
<br />
雨林木风公司负责人赖霖枫向网易科技透露，“Ylmf OS”系统是雨林木风涉足开源项目后全力支持的重头戏，未来也会作为雨林木风的长期战略项目之一，和现有的其他收费操作系统一样会持续发布更新版本，也将为合作企业提供一体化技术解决方案，并将其延伸到雨林木风的实体项目中。<br />
<br />
2008年12月26日，雨林木风工作室对外发布了一份公开声明，宣布在2008年12月31日正式解散。工作室解散后，雨林木风不再对不开源的操作系统(Windows XP、Vista即属此类)进行技术研究，将以自主研发软件产品为主。<br />
<br />
据了解，在工作室解散后的一年里，雨林木风努力尝试向自主研发转型。今年3月份，雨林木风宣称会投入过亿资金发展115项目，然后5月份推出了免费存储平台115网络U盘，同时启动了114啦网址导航开源项目，年底再接再厉以XP.com推出Ylmf OS开源操作系统，加上既有的网址导航门户114la.com；雨林木风在短短一年时间内构建了云存储、操作系统和网址导航的产品线布局，由此聚合的海量用户将成为雨林木风未来成长的基础。（古丰）<br />
