													<h2>真理部陷害Google的更多证据</h2>
						
						
																											<div id="postmessage_137107" class="t_msgfont">Google，你不应该服务中国大陆<br />
写于2009-06-25 C 3:30 pm | 作者:Jason Ng | 类别 &raquo; 搜索引擎 <br />
昨晚9点左右，陆续有中国大陆的网友反映无法打开google.com旗下所有服务，就连ggpht.com、googleapis.com这些看起来和google.com联系并不多的网站都无法在中国大陆访问。<br />
<br />
到了晚上11点左右，各地陆续恢复，先是Gmail，接着Docs，再后来基本上所有的google.com服务都可以正常访问。但是，根据我在twitter上看到的情况，还有不少线路目前仍然无法访问Google.com（所以处在这些线路的朋友们也看不到这篇文章的图片）。<br />
<br />
从我目前获得的信息，这一次Google.com访问被中断，和Google没有关系，幕后是谁，大家心里都知道。<br />
<img src="http://lh5.ggpht.com/_RmRZZIr3Kws/SkMmKrbzyjI/AAAAAAAAEWM/2pGooy4GVBE/s800/china.png" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
(pic via)<br />
<br />
一、有组织、有预谋的央视 <br />
Google搜索框里出现的“搜索建议”下拉菜单里的关键词是由搜索频率决定的，也就是说，如果在一定时间内，某些关键词的搜索频率相对较高，那它们就更有机会出现在搜索建议的下拉菜单里。比如输入“可能”，搜索建议里就会出现“可能吧”：<br />
<img src="http://lh4.ggpht.com/_RmRZZIr3Kws/SkMmPsEyV-I/AAAAAAAAEWg/wTCIggqjwpQ/s800/snap00027.png" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
昨天有人在Google Docs上发布了一篇文档，文档指出：央视曝光谷歌搜索含有色情信息的报道里，搜索框出现的搜索建议是有人故意刷出来的。文档里用了“儿子”开头的关键词，其实那还不足够指出央视的“有组织、有预谋”，让我们看看另外一些关键词。<br />
<br />
在《焦点访谈》里，互联网违法与不良信息举报中心的奚伟副主任说，在google的搜索框输入“性”，google就会给出一大堆的“联想关键词”，搜索这些关键词都能引导到“黄色网站”。视频截图如下：<br />
<img src="http://lh6.ggpht.com/_RmRZZIr3Kws/SkMmK00feHI/AAAAAAAAEWc/ACMRT-sjiG0/s800/snap00026.png" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
<br />
姑且不谈“联想关键词”的表达错误，我们将视频里出现的关键词“性开放图片”放到Google Insights for Search里看看搜索频度：<br />
<img src="http://lh6.ggpht.com/_RmRZZIr3Kws/SkMmP_xbw0I/AAAAAAAAEWk/zYkwr0c5L6Y/s800/snap00028.png" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
<br />
关键词“性开放图片”在17日、18日的搜索量突然飙升，之前几乎是没有搜索量。而19日的搜索量依然较高可以理解为央视播出节目后有不少人去尝试搜索这个关键词。<br />
<br />
而央视的节目是在18日晚上播出的，这里面有什么逻辑呢？先不要急于下结论，继续看奚伟副主任指出的另外4个关键词，为便于观察，我将截图里的前五个关键词放到一个图表里，数据范围为全中国：<br />
<img src="http://lh5.ggpht.com/_RmRZZIr3Kws/SkMmK8oG_dI/AAAAAAAAEWY/dzDkgZwZaD4/s800/search_Vol_cn.png" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
<br />
还是看不出问题所在？我们将区域缩小到只看北京地区的：<br />
<img src="http://lh5.ggpht.com/_RmRZZIr3Kws/SkMmK2oisGI/AAAAAAAAEWU/v3FpmGFsNAM/s800/search_Vol_bj.png" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
<br />
问题出来了，奚伟副主任键入“性”后出现的建议都是在17日、18日被刷出来的。更巧合的是，这5个关键词，在全国范围和在北京范围的搜索频率曲线惊人的吻合。这说明了什么呢？<br />
<br />
这里面的逻辑就非常容易想到了，央视为了让Google在报道中看起来“很黄很暴力”、让人“心神不宁”，在报道的前两天故意去刷这些关键词，到了真正拍摄的时候，这些搜索建议就很自然地出现了，央视就可以大声地说：“看吧，Google就是个色情搜索引擎”。<br />
<br />
二、大量舆论攻势污蔑Google <br />
为了配合笑话-“绿坝”的推行，有关部门真是大费脑汁，动用了大量的舆论攻势，正如我之前在“央视曝光谷歌搜索含有大量色情淫秽内容的逻辑”里所说，只有拿Google开刀，才能让无知网民、不明真相的群众认为互联网是淫秽满布、让人心神不宁。<br />
<br />
让我们围观这些看起来是五毛，实际上不止五毛的舆论吧：<br />
<br />
1、人民网：“谷歌中国”网站传播低俗内容引发热议<br />
<br />
2、人民网：“技术致黄说”是捧谷歌的臭脚<br />
<br />
3、人民网：谷歌“不作恶”口号沦为遮羞布<br />
<br />
4、新华网：谷歌自甘堕落，处罚惩前毖后<br />
<br />
5、CCTV：叫醒”打盹”的谷歌乃共赢之举<br />
<br />
6、中国青年报：谷歌”无辜”姿态蒙蔽了多少人<br />
<br />
7、人民网：警惕谷歌假整改蒙混过关<br />
<br />
8、人民网：“谷歌”传播淫秽色情害人又害己<br />
<br />
9、人民网：谷歌中国仍在色情漩涡中 所链英文站“导黄”更甚<br />
<br />
10、人民网：用法律利剑斩断谷歌中国的“色情之结”<br />
<br />
部分链接收集来源于白板报。<br />
<br />
甚至，今天新华网做了个让人心神不宁的调查，截图如下：<br />
<img src="http://lh3.ggpht.com/_RmRZZIr3Kws/SkMmP7FglXI/AAAAAAAAEWo/bv0iGUPzKP0/s800/snap00033.png" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
<br />
这个调查，无论从选题还是答案，都是值得竖起“拇指”的。<br />
<br />
三、不听话，就断网 <br />
Google在中国是非常不听话的企业，根据新华网的报道：<br />
<br />
新华网北京6月19日电（记者卫敏丽）记者从全国整治互联网低俗之风办公室获悉，国家有关部门18日下午召见“谷歌中国”网站（google.cn）负责人，对“谷歌中国”网站大量传播淫秽色情内容进行执法谈话，宣布对“谷歌中国”网站的处罚措施，暂停该网站境外网页搜索业务和联想词搜索业务，并责令其立即进行整改，彻底清理淫秽色情和低俗内容。 <br />
<br />
有关部门要求谷歌“暂停境外网页搜索业务”，这不，现在在google.cn依然能搜索到维基百科、CNN这些外国反动网站，谷歌你这么不听话，竟敢公然抗旨，不断你网断谁？<br />
<br />
昨晚的断网事件是一个警告，谷歌你要是再不听话，后面还有更好玩的。<br />
<br />
四、 Google.com无法访问带来的后果是非常严重的 <br />
Google.com无法访问带来的后果有多严重？<br />
<br />
0、显然，搜索用不了，地图、阅读器也用不了。<br />
<br />
1、所有挂有Google Adsense的网站无法正常加载，广告收入减少甚至消失，如果adsense长期无法使用，这些网站可能会关闭<br />
<br />
2、调用Google API的网站均无法正常加载<br />
<br />
3、网站上含有的picasa相册图片均无法显示<br />
<br />
4、Gmail无法访问，重要邮件无法读取<br />
<br />
5、更严重的是，google.com的域名解析被劫持，国内邮箱无法与Gmail进行通信<br />
<br />
6、大多数与IT相关职业的人必须使用google，google无法访问将可能导致工作无法完成，社会财富可能因此减少<br />
<br />
总之，一旦Google的业务无法正常访问，后果是不堪设想的，尤其是Gmail的通信被切断的话，那简直是暗淡无光的日子。<br />
<br />
可以说，封锁Google是站在网民的反面，是在历史潮流面前螳臂当车。<br />
<br />
五、如何判断google是否被封？ <br />
当你无法访问Google时，不要惊慌，先看看到底是本地电脑的问题，还是全国性的问题。这是我的建议：<br />
<br />
1、回忆自己是否搜索了一些不和谐的词语，如果是，那就等3分钟再尝试<br />
<br />
2、看看加密的Gmail或GReader能否访问，如果是，那问题还不算严重<br />
<br />
3、换个浏览器试试<br />
<br />
4、稍等几分钟看看是否能恢复访问<br />
<br />
5、切换到OpenDNS看看是不是本地解析错误，OpenDNS的IP是：<br />
<br />
208.67.222.222<br />
<br />
208.67.220.220<br />
<img src="http://lh3.ggpht.com/_RmRZZIr3Kws/SkMmQFvrO-I/AAAAAAAAEWs/iKi1ObdzhQw/s800/snap00034.png" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
<br />
6、在twitter看看有没有其他人说google无法访问，最好的是搜索“google”，如果真的是大面积访问失败，肯定很多人在讨论这个话题<br />
<br />
7、使用外国代理看能否打开Google，如果能，则有可能是中国大陆的问题<br />
<br />
8、如果Gmail在全国都无法访问，这意味着问题很快会解决，因为Gmail不可能长期被水产<br />
<br />
六、翻墙是每个中国网民应掌握的技能 <br />
种种迹象表明，我们的国家正在组建全球最大的局域网。要获得多维的信息，我们必须翻墙到外面的世界，我相信可能吧的读者们都有自己的翻墙方法，这里无需讨论也不能讨论。<br />
<br />
每一次的“网络故障”，都会使一批新人学会翻墙；每一次的舆论导向，都会让人获得更多真相。<br />
<br />
1.0方式的管理不能制止2.0方式的传播，信息总有发散地、无中心地传播着。<br />
<br />
如果你身边有人不知道墙，不要笑话他，设法让他很想上某个网站但又怎样都上不去，这时给他一个“梯子”，他就明白墙的存在了。这一直是我的做法。我的经验告诉我，直接和他们说“墙是存在的”是没用的，要让他们亲自撞墙才能深切体会。<br />
<br />
随着大中华局域网的建成和绿坝的推行，翻墙必定成为每个中国网民必须熟练掌握的技能。<br />
<br />
七、谷歌的无奈 <br />
google.cn作为Google.com的“据当地法律法规和政策，部分搜索结果未予显示”阉割版，承受的压力比我们的鄙视要多。<br />
<br />
一方面它承受着来自中国政府的压力，要求将一些“不良”结果删除。<br />
<br />
另一方面，它还承受着美国要求公正提供搜索结果的压力。<br />
<br />
第三方面，它还因为自我阉割而承受着高端网民的鄙视。<br />
<br />
谷歌有多无奈或许只有它自己知道，这不是它的错，问题不在于谷歌，而在于幕后的大老板。<br />
<img src="http://lh5.ggpht.com/_RmRZZIr3Kws/SkMmKg57fOI/AAAAAAAAEWQ/tMFYTUSIv9Y/s800/google-china.png" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
(pic via)<br />
<br />
谷歌今年在中国有了上升的势头，一方面和百度的竞价门有关，另一方面则因为其努力的本地化，从拼音输入法到谷歌导航，再到音乐搜索，这些都是本地化的产物。<br />
<br />
然而，当我们都以为谷歌在中国终于有了翻身的机会时，它却遇到了不可抵抗的力量，连李开复在电信3G广告里的镜头都被张朝阳替换掉了。谷歌的无奈与悲哀我们应该去理解，而不是去抱怨、指责它，因为它反抗不了大老板。<br />
<br />
或许，它本不应该进入中国大陆这片神奇的土地。<br />
<br />
但不管怎样，作为一个热爱互联网、热爱自由的网民，我义无反顾地支持Google（谷歌）。<br />
