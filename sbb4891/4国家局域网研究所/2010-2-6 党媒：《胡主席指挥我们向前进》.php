													<h2>军歌肉麻吹捧胡锦涛</h2>
						
						
																											<div id="postmessage_475156" class="t_msgfont">一首《胡主席指挥我们向前进》的军旅歌曲，在被冷落两年多后，近期突然备受关注，不只出现在多个音乐网站，还出现在各式各样的论坛、私人网志，肉麻吹捧的有之，冷嘲热讽的有之。<br />
&nbsp; &nbsp;听这首歌，第一感觉是时空错乱，光是歌词就令人起鸡皮疙瘩。歌词反复歌唱战士热爱、崇敬、想念、拥戴胡主席，「胡主席领导我们解放军，胡主席指引我们向前进......」。很多人不期然想起文革歌曲《我爱北京天安门》中的「伟大领袖毛主席，指引我们向前进」一句，但新歌的曲调又没有文革歌曲的激情，更没有军旅歌曲惯有的雄壮，感觉就像无病呻吟。<br />
&nbsp; &nbsp;这首歌据称是「解放军官兵献给党的十七大的赞歌」，即出现于2007年10月前后，来头也不小：奏弦乐的是中国国家交响乐团、爱乐交响乐团，奏管乐的是爱乐交响乐团，合唱的是解放军总政合唱团。不过，歌曲推出后显然被冷落了，至2008年初才被个别网站提及，此后也未见官方宣传。<br />
&nbsp; &nbsp;直至去年中共建政60年大阅兵后，这首歌被配上胡锦涛在长安街检阅部队的视频，重新推出，加上《解放军报》图文并茂报道一些军队教唱这首歌的消息，终于引起关注。军旅歌曲如此肉麻地吹捧一位领导人，只出现在毛泽东年代，在邓小平、江泽民主政时代也未见，而歌曲由推出到被冷落，再到被吹捧，也映证了两代军委主席江泽民与胡锦涛的权斗历程。<br />
<br />
&nbsp; &nbsp;中共十七大是胡锦涛主政后的第一次党代会，江泽民完全退出领导层，于是乎有人创作《胡主席指挥我们向前进》应景，但「胡核心」未能加载中共文件，歌曲似乎也没用武之地。去年「十一」庆典，江、胡同时亮相天安门城楼阅兵，权斗白炽化，歌曲视频版的推出，显然是要扬胡抑江，而军方报章的介入，则显示胡锦涛占了现任主席之利。只不过，如此肉麻的吹捧，就算胡锦涛自己受落，也太罔顾社会观感，只会适得其反。<br />
