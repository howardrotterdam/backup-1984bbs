OpenDNS历史<br />
OpenDNS 在 2006年7月由 黑客／创业者 大卫・尤里维奇（David Ulevitch）创建。之后获得了由CNET的创始人Halsey Minor创建的Minor Ventures公司提供的风险投资。<br />
<br />
2006年7月10号，这项OpenDNS开始为digg[1]、slashdot[2]和Wired News[3]网站提供服务，这直接导致DNS请求数从7月9日的一百万猛增到30日的三千万。[4]<br />
<br />
2006年10月2日，OpenDNS建立了第一个反钓鱼数据库Phishtank.com[5]，收集、整理和发布钓鱼攻击信息。<br />
<br />
2006年，OpenDNS开始使用DynDNS的接口来处理动态IP用户的DNS更新。[6]<br />
<br />
从2007年1月开始，OpenDNS开始在以下地区设置服务器来提供服务：西雅图、帕洛阿尔托、纽约、华盛顿和伦敦，并计划扩展到芝加哥和香港。<br />
<br />
2007年6月11日，OpenDNS开始启用高级网页过滤系统来为他们的免费账户过滤成人内容。<br />
<br />
<br />
服务<br />
OpenDNS为个人和商业提供DNS方案，用户可以自行选择使用OpenDNS的服务或者使用当地ISP提供的DNS服务。将服务器组放置在具有战略意义的地方和使用大量的域名缓存可以使DNS查询进度可以更快得多地完成，从而加快页面的检索速度。DNS的查询结果有时被本地的操作系统或应用程序缓存下来，所以速度的增加也许不能在每次查询中体现出来，但本地缓存里没有的结果其查询速度的增加则显而易见。 其他特征包括一个反钓鱼过滤器和输入纠正（typo correction）〔举例说明，你输入wikipedia.og会被自动替换成wikipedia.org〕。通过收集恶意网站列表，当用户通过他们的服务来访问这些恶意网站时，OpenDNS将封锁这些恶意网站。OpenDNS最近启动了反钓鱼服务（PhishTank），这样全球的用户就可以报告和察看不可信的钓鱼网站。<br />
