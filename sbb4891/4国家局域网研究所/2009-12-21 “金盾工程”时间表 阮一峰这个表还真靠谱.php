													<h2>“金盾工程”时间表 阮一峰这个表还真靠谱</h2>
						
						
																											<div id="postmessage_386376" class="t_msgfont"><a href="http://www.ruanyifeng.com/blog/2009/06/timetable_of_china_golden_shield_project.html" target="_blank">http://www.ruanyifeng.com/blog/2 ... shield_project.html</a><br />
<br />
“金盾工程”时间表<br />
阮一峰 发表于 2009年06月 9日 | 分类：首页 -&gt; 档案 -&gt; 互联网<br />
<br />
<br />
2009年<br />
<br />
中国政府宣布境内销售的所有计算机，必须预装绿色上网过滤软件，以便过滤互联网上的不良文字和图像。<br />
<br />
2010年<br />
<br />
中国政府宣布网站备案制度与“绿色上网”相结合。境内所有没有备案的网站，都会被软件过滤，无法浏览。<br />
<br />
2011年<br />
<br />
中国政府宣布“网站备案制度”扩展到境外网站。凡是在中国境内可以访问到的境外网站，视同在华开展业务的外国机构进行管理，适用相关的外国机构管理法规。境外网站如果想在华开展业务，必须向中国政府进行备案，得到批准后才能开展业务。<br />
<br />
2012年<br />
<br />
中国政府宣布启动域名备案，境内所有域名都必须向政府备案。<br />
<br />
2013年<br />
<br />
中国政府宣布启用域名白名单制度，即只有在“白名单”中的域名，才提供解析服务。境内和境外网站在华一旦备案成功，其域名将自动加入“白名单”。<br />
<br />
2014年<br />
<br />
中国政府宣布对所有互联网上使用的加密证书进行备案。不管是公钥还是私钥，都必须在政府报备。如果加密通信中，使用了没有报备的密钥，电信公司的网关将自动拦截，阻止其通过。<br />
<br />
2015年<br />
<br />
中国政府宣布实行电子邮件实名制。凡是需要开通电子邮件的公民和企业，一律凭相应证件到政府指定的电子邮件服务商处申请。如果一封电子邮件的发信域，不在有合格资质的电子邮件服务提供商的列表之中，该封邮件将被自动退回。<br />
<br />
2016年<br />
<br />
中国政府宣布实行IP地址实名制。那个时候，IPv6已经得到广泛应用，IP地址不再是稀缺资源。根据中国政府的设想，每个公民一出生，就可以分配到一个IP地址，终生保持不变。然后，每个人上网，都必须使用自己的法定地址，不得擅自使用他人的地址。<br />
<br />
2017年<br />
<br />
中国政府向全世界宣告，“金盾工程”初步完成，中国已经建成了世界上最安全的互联网。<br />
<br />
