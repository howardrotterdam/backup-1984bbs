二、翻墙<br />
<br />
下面来说说大家最关心的，那些能上网的新疆人，是怎么从铁桶一般的墙里翻出来的。<br />
<br />
首先，内地公网使用的翻墙方法完全不适用于新疆，因为封锁的原理不同。新疆现在就是一个大局域网，外网根本就不通，你提供哪些国外的翻墙网站或者翻墙软件，自然只会404 not found或者service unveilabe。现在已知的是如下几种方法：<br />
<br />
1、窄带拨号：<br />
这个是应用最广泛的拨号方式，你不需要有特权，也不需要上面有人，只要你有一个破旧的56k窄带猫，连上电话线就可以。虽然速度很慢，开个新浪都要等2分钟，但是，那是外网啊（请自行脑补加入流泪233表情）！当然，你不能拨本地的窄带接入号，那一样只能访问疆内网。<br />
<br />
流行的拨号号码有这些：<br />
<br />
022-16300、0891-16300：8月起，这两个号码造福了很多网民。稳定，但不容易拨入，平均拨号5次能够连接上，10月份被封。<br />
<br />
010-95700：10月到12月间，这是最流行的拨号接入号，拨通率非常高。如果你看到有人家里固话电话费突然升到好几百元甚至上千元，那不用问，肯定是天天95700呢。这个号码12月初被封。<br />
<br />
0756-96169：95700不在的日子，我们又找到了96169。可惜没能坚持1个月就被封了。<br />
<br />
友情提醒：拨长途窄带ISP号码前请加拨11808，这样一小时话费能够控制在5元以内，否则请按长途标准资费（大约1分钟7毛钱）换算自己的电话费。<br />
<br />
<br />
2、企业内网：<br />
大家知道很多大型企业的内部网络都是全国连通的，电信运营商自不必说，其他包括铁路、石油、银行等都有自己的全国性网络，由于各企业的业务需要，这些内网是不可能断掉的。这就给通过内网翻墙带来了可能性。只要在其他省份找到一台同在内网且可以上网的机器，于其上开个代理，便可以顺利上网。一时间，“内地同事”成了抢手货，疆内软件网站上ccproxy、wingate这些代理软件也都上升到下载榜前列。<br />
<br />
这种翻墙方式网速尚可，实现难度也不算太高，也算是比较流行的翻墙方式。当局下强硬命令要求各单位逐机清理的7-5视频，大多也是这种方式流传出去的。<br />
<br />
<br />
3、海事卫星等卫星通讯方式：<br />
卫星通讯方式的特性决定了它不可能像有线网络一样被地区性中断，有条件的单位和个人，可以通过这种方式翻墙。不过这种方式网速不怎样，而且资费极贵，所以基本只有少数单位在使用。<br />
<br />
有次翻墙出来上QQ遇到一个疆内的朋友，问他怎么上网的，便给我炫耀其单位特权申请了卫星专线4条，办公室电脑都能上网了云云。<br />
<br />
<br />
4、特权<br />
在中国，任何事情的限制范围，都可以加一条：领导例外――就连断网都断得这么有中国特色。除了通讯管理局、公安厅、安全厅这些跟互联网直接有关的单位可以上网外，据我所知还有各通讯运营商处级以上领导，当局机关的部分部门，疆内官方大站的运营部门（新丝路、天脉、天山网、亚心网、乌鲁木齐在线），均可以顺利上网。当然，各单位都有相应的管制手段，如限制部分端口，限制QQ登录等，但还是让普通百姓羡慕不已。<br />
<br />
当然，普通企业和个人“原则上”也可以申请开通，但除非你能通过电信运营商-通讯管理局-公安/安全/自治区当局多个部门层层审批通过才行，至今通过的公司寥寥无几，个人更是不要奢望了。<br />
<br />
<br />
5、异地上网方式<br />
之所以新疆能被这么彻底的断网，跟新疆的地理位置也有很大关系。如果内地的某个省份出事被断网，网民很容易就能到别的省去，而且内地错综复杂的线路也不是能说断就断的。新疆的骨干网出疆端口只有两条，很容易就能封锁。如果你想出去外省，哪怕是到最近的甘肃，那一千多公里的路程至少也得1整夜的火车。<br />
<br />
但即便这样，仍然有难以忍耐的网民跑去外省上网。BBC就报道过一个新疆人坐飞机去深圳上网的事情。而新疆本地QQ上流传的一条信息则是：“柳园一出车站就有好几家网吧，我去过了。网吧对面就是旅馆，下火车出站就可以上网了。那个网吧全是新疆这边的，本地人基本没与几个，到了晚上8点全是新疆的网民。包夜8 块钱。10点到早上9点。就这些了。过年时候我也要去 我的电话136699xxxxx要去一起呵呵！”――柳园是甘肃省离新疆最近的一个小火车站。<br />
<br />
还有一则未经证实的消息，是说有人利用手机基站信号的边界重叠，去新疆东南边的若羌县用青海省的3G信号上网。之所以说未经证实，是因为那里是维族人口占90%以上的民族聚居地，就算能3G上网，但小命不一定能保全啊……<br />
<br />
<br />
以上基本就是新疆境内翻墙的方法，发出来给对网络习以为常的里层JR们看看，新疆人民上个网是多么难。有的人QQ密码忘了，有的人苦心经营的网站废了，连菜地都快半年没收成了……去内地出差回来的同事说：“你知不知道当我输入三达布溜淘宝点康木一回车，看到那熟悉的黄色页面出现时，才真正的体会到了什么叫这一刻我内牛满面……”<br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
三、现状<br />
<br />
断网前新疆 90%以上的互联网流量都是流向内地的，但现在这个大局域网已然成了私服和山寨网站的乐土。各种网游私服层出不穷；利用FICQ和RTX搭建的新疆QQ也有N个版本了；山寨开心网不下10个，虽然用的都是同一套源代码连界面配色都一模一样；hao123类的疆内网站导航已知的就有几十个；甚至连百度和 Google都被山寨了（参见附图），虽然做的有够烂……最近最火的，则是一个叫zn11的网站，点击率超级高。这个网站是干什么的呢，说出来外面的各位可能会哑然失笑――就是用teleport把新浪、腾讯、网易等几个大站的首页和2、3级页面离线下载，然后挂到站上。虽然可能几天才更新一次，大量链接点不开，视频也无法收看，但还是有很多人上班第一件事就是打开这个zn11。也许是让自己觉得离真正的互联网近一点吧……<br />
<br />
这是一个最好的时代，这是一个最坏的时代。对于私服和山寨网站运营者来说，显然是前者。但对于电信运营商来说，很有可能是后者。虽然广播报纸上电信移动联通三家的3G广告还是铺天盖地，但是谁都会对“WCDMA，上网速度可达7.2兆”的广告一笑了之，网都没了推广什么3G啊。损失最重的是主要收入来自于宽带业务的新疆电信，7月份全疆宽带费用全免，一个月直接损失5000多万，关联损失8000多万。8月份以后宽带费用采用用多少交多少不用不交，最多收取原费用8折的方法，但已然抑制不住每月飙升的宽带拆机量了。<br />
<br />
上不了网的不方便，很多人已经渐渐习惯了。官方大站上不少“没有互联网的日子，我过得更好了”这类的傻逼帖子，甚至我朋友里也出现了斯德哥尔摩综合症患者。但我想大家更为担忧的，应该是TG在观察过新疆百姓对三断的反应之后，会不会把这个损招用在其他省份，甚至全国。<br />
