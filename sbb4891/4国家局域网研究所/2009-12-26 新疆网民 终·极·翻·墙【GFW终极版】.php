																											<div id="postmessage_397656" class="t_msgfont">一、断网<br />
<br />
现在新疆全境依然是断网断短信断国际长途三断中。这种中断跟内地的GFW不尽相同，对于普通用户基本上算是物理隔离，几乎所有外网ip和外网端口均无法访问。能够访问的只有疆内的站，以及极少数ip和主机在外地的国家级官方网站（如网站备案查询、各种考试报名网站）。也就是说，国内公众网的GFW是默认放行，部分ip和域名拦截+关键词拦截；而新疆是默认断开，个别ip和域名放行。<br />
<br />
上网方式方面，7月6日凌晨4点chinanet全部封锁，随后是移动、联通的公众网络。教育网（CERNET）估计是用的电信出口，也是同步被封。科技网（金桥）多撑了两天，于7月9日被封锁。手机上网方面，NET方式均同时被封锁，WAP方式则推迟到七月十几日左右才封锁，其中电信的WAP方式甚至到了 8月中才封锁完毕，期间一直可以登录WAPQQ。3G上网卡方面，基本是同时封锁，有传闻是如果用天翼3G拨号时获得的IP是120.X.X.X就可以顺利上网，不过这个传闻未验证。普通用户能够翻出GFW终极版的上网方式只有拨号一种了，也就是10年前我们大家常用的56k窄带拨号。具体方式后文详述。<br />
<br />
短信方面，点对点短信均无法发送，手机端直接报错，应该是点对点短信网关根本就没启动。仅有部分公众SP业务开通，如天气预报、手机报、 10000/10086/10010这类的运营商官方信息，用户可以接到。需要注意的是外地手机漫游至新疆，一样无法发短信，所以不要妄想买个外地短信卡就能在新疆聊天了。我自己曾经发短信如飞的手指，现在也快不知道怎么按了。可以说这几个月新疆的SP和CP类公司遭到了重创，倒闭关门或撤回内地的不计其数。<br />
<br />
国际长途方面，大概是7月7日开始就非常难以拨入，8月以后就基本无法拨入了。呼出方面，除了当局指定的仅有几个机关单位和几个大的电信营业厅可以打（像不像80年代），其他全部的直播、IP、网络国际长途电话均无法呼出。批发国际ip卡的商家亏到出屎，新疆大批做中亚外贸的商家也都难以开展业务。我的表妹在马来西亚上学，快半年了没一个电话，她老妈被逼的请了假去内地上网，终于在QQ上得以一见。<br />
<br />
7.5后的这些封锁，除国内长途封锁时间较短，7.5后几天即解除外，其他的直到现在（12月25日）也没有一点儿放松的迹象。基本每个月都有传闻说这个月/下个月要开网，什么时候开网也是疆内各论坛最常讨论的一个话题，已经不能用日经来形容了。可惜这些期望，最后都是以失望告终。<br />
<br />
其中有这么几个著名的传闻：<br />
<br />
断网持续48小时：这是7月6日最初的传闻，据说来自于运营商。可惜随着7月7日汉族大规模游行示威告吹。<br />
<br />
断网一周：参照石首，这个传闻很快告吹。<br />
<br />
断网半个月/一个月：说西藏314时断网就这么久，新疆也会参照。可惜据我本人考证，西藏当时似乎没有断网，或断网时间很短。在Google上搜“西藏 断网”，貌似没有什么有价值的信息，希望有了解情况的JR说说。<br />
<br />
10月国庆后开网：这个传闻是说都60大寿了，安定和谐的天朝肯定会给我们这2000w草民开网。告吹。<br />
<br />
11月1日开部分门户网站，春节放开全部：这个传闻是最有模有样的，据说是自治区级领导安排，大部分运营商都内部通报了。可惜现在马上新年了，仍然没有一个门户网站能够访问。这个传闻应该为真，据说最后是自治区政法委书记符强同志强力压了下来。<br />
<br />
最新的传闻是说1月份要开放部分门户，全部放开到2010年5月份左右：这个消息同样来自运营商内部，然则已经没有多少人关心了。对通网的传闻新疆的网民早已麻木，对这类消息都是无奈的一笑了之。<br />
