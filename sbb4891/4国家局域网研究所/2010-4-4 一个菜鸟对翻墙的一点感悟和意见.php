																											<div id="postmessage_547171" class="t_msgfont">最初接触翻墙大概是在三年前，那是百度贴吧繁盛的日子（那个山寨版的初级WEB2.0倒成为了我摆脱传统认知方法的“导师”，而那也是网络改变我生活的起点。所以我以为国内的山寨并非一无是处），通过对那个伪WEB2.0的访问，我深深卷入到了网络上的争论，并由此结识了一大批在现实世界中很难认识到的朋友，那个时候，墙还不高，翻墙软件是那么唾手可得，翻墙的方法是如此简单易学，以至于我回到学校后，竟然只要用百度下个自由门之类的软件，以傻瓜的方式点开则可轻松翻越!现在，每个网络上的人都可以感受得到，网络环境是变得多么的严酷，人们讲真话变得越来越困难，墙在不断的砌高！而能改变这种情况的唯一办法就是大力普及翻墙技术！（不要指望某政权会自废武功）从大处说，如果翻墙技术得不到普及，整个民族的前途就很可忧虑，因为信息的自由流通正是技术得以发展的重要前提；从小处说，中国正在形成中的公民社会很可能会被因此扼杀！（在此，我不赘言历史和政治学的知识了）从个人来说，你愿意成为未来信息时代的二等公民吗？下面就是我这么些年来作为菜鸟对于翻墙技术普及的一点建议：<br />
一、界面要简单，傻瓜式的界面是最好不过，千万不要有各种各样的夹私（那很烦人，比如某个政治组织所做的）用网页的形式翻墙是最好不过的。赛风计划就是一个很好的尝试，虽然现在被封了。人们有心却没有力去学习繁杂的翻墙技术，如果你界面还很复杂，那么人们就彻底丧失了耐性了。<br />
二、形式要多样，要针对不同的网络环境。现今我所见到翻墙技术（尤其是傻瓜型软件）绝大部分是针对的是家用型，而校园网呢？网吧呢？无线环境呢？你要知道，只有适用于绝大多数环境的技术才有可能得到普及，成为常识。推特就是一个成功的例子，一百多个中文介入方法，你想封也封不住。如果能做到有网的地方就可以翻墙，那么GFW基本就以失败告终。<br />
三、加强引导菜鸟型的新手升级和高手之间的协调工作。一个木桶的最低的那块木头才决定桶的容量！如果没有新手的成长，翻墙技术恐怕永远也跟不上GFW和谐的速度，而我们只能穷于应付。<br />
拉拉杂杂谈了这么些，都是作为菜鸟的一点肤浅感受和意见。我相信翻墙技术虽然面对的是GFW这头巨兽（维稳费用竟然超过军费！)可是胜利最终还是属于我们的！因为民心在我们这边！</div>

							
							
							
							
