																											<div id="postmessage_118719" class="t_msgfont"><img src="http://www.go-gddq.com/upload/2006_07/060708113215461.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
抗短波干扰方法许多听众来信或通过电话告诉我们收听广播受到干扰给他们带来的困惑。这里向大家介绍一种减少干扰的方法，您不妨试一试。<br />
<br />
一. 材料短波收音机一台 天线一支(要比收音机天线短2cm,也可用其他电线代替) 三合板一块(面积约30x60cm) 小钩子四个 锡纸两张(也可用其他导电材料代替)，尺寸比收音机面积稍大一点小木头一块(面积约4x4x2cm) 电线两段(约10cm) 小螺丝钉两个，橡皮筋两条<br />
<br />
二. 程序<br />
<br />
1.将两块锡纸分别按图上的位置平贴在三合板上(锡纸不能相交)。将四个钩子分别钉在放收音机的锡纸四周(用来固定收音机)。<br />
<br />
2.在木块上钻一个与天线底部直径相当的孔。插入天线，其底部要连接电线。电线的另一端用螺丝钉固定在放收音机的锡纸上。将木块按图所示位置固定在三合板上。<br />
<br />
3.用螺丝钉将电线的一头固定在另一张锡纸上，另一头缠绕在收音机的天线上(如图所示)。<br />
<br />
4.将收音机按照图上等样子平放在锡纸上，用两根橡皮筋十字交叉将收音机固定在板子上。这时，收音机的天线及第二支天线同时处于垂直三合板的位置。收音机的天线全部拉出，第二支天线的高度应稍低于收音机的天线。这样，系统就完成了。<br />
<br />
5.将收音机调到要听的频率，然后放置在一个相对空旷的地方(如窗户旁)。旋转三合板的角度直至找到最佳收听效果。旋转时手不能触摸锡纸，收音机及天线。<br />
<br />
6.以上方法如得不到良好的收听效果，还可先调整收音机天线的角度，然后再进行旋转。以致得到最佳收听效果。如一个频率不行，要调到另一个频率，重复以上步骤，以达到最佳频率的最佳收听效果为止。注：如果干扰信号与广播信号来自同一方向，这一系统很难有效。</div>

							
							
							
							
