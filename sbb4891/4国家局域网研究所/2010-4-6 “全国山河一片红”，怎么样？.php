<br />
是这样。<br />
<br />
在豆瓣上发《克拉玛依》死难者名单和导演专访，被封帐号了。<br />
<br />
由此想到一个问题。<br />
<br />
遮羞布、国新办、各省网监部门发布的敏感词，貌似有越来越多的常用词？<br />
<br />
比如地名，远的有乌鲁木齐、拉萨、石首、瓮安，近的有克拉玛依等等；常用词，比如谷歌、西厢、温习、胡萝卜和其他什么24嘴口交机这些切口（澹。<br />
<br />
当初西厢计划刚出来，有人就议论说，给翻墙计划、翻墙软件起名字，最好用常用词，这样就能让一些人在无意中搜索到。<br />
<br />
这里的问题在于，网监监控敏感词所需要的人力物力不是无限供给，他们需要花钱雇人来删除、监控。如果更多的常用词和地名成为敏感词，每个敏感词是个“红叉”，那么，“全国山河一片红”就真正实现了。<br />
<br />
“全国山河一片红”哪怕部分实现，中国的网络就将极不稳定了。比如，地名是最常用的一类词汇，是个地名就监控，就自动跳到后台由版主、管理员审核，大家的正常交流就会受到很大影响。我们发帖可以用同音字、加标点符号，但正式的文章、文件是不能这么做的（比如前一阵子，广西新闻网就因为网站里有胡逄味被自己的系统自动关闭，谁也不能进入）。<br />
<br />
<strong>换句话说，【1】网民其实是可以利用敏感词来拖垮网络的。</strong>当然，由此造成的不便，网民是完全无责任的。谁也没同意他们把纳税人的钱用来监控我们那些普通正常的言论。<br />
<br />
<strong>至于如何拖垮……还没想到……</strong><br />
<br />
另外，有趣而且很有意义的一个现象是，敏感词并非完全是全国统一发布。这就意味着，A省或全国都正常使用的词汇，在B省偏偏就出了问题，成了敏感词。这意味着什么呢？这就意味着敏感词的出现是完全无法预料的，而这种各省市各自发布敏感词的混乱局面，应该会随着各种地方性事件的出现而不断升级。<br />
<br />
事实上，全国通用的敏感词并不很多，而各个地方发布的禁区、敏感词才是主力。至少前一两年是这样。（从某渠道了解的）<br />
<br />
<strong>【2】这种不断出现的混乱局面，必然干扰正常的信息交流。而这正是可以借此利用的机会。<br />
<br />
牛人发挥才智吧。</strong><br />
<br />
----------<br />
