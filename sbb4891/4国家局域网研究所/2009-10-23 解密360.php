领域基本差不多了。手机是下一个增长点。<br />
PC上隐私有限，手机上用户的隐私更多，更值钱，无数短信群发公司已经证明了这一点。360<br />
要进入手机领域，成为移动互联网“黑社会”老大，总领手机软<br />
件公司生杀予夺的大权。<br />
<br />
9. 为什么做扣费克星？<br />
从扣费克星的查杀列表可以看出周总在手机上沿用了互联网上的思路痕迹：实施焦土战略<br />
，先扫清手机上的常用小软件；然后，开始收保护费，顺我者昌，逆我者<br />
亡。 “扣费克星”这么响亮，当然要加入恶意扣费软件。但是，手机大头、千尺、来电秀、<br />
许多无线SP公司的商用收费软件大都被加入列表。想在手机上发展<br />
又没有很强靠山的公司，要注意了。<br />
周总一般先不会得罪大的公司，有不少大公司的软件一样是收费的，但是名单中并没有。<br />
联合少量大的，打击小的，这是做流氓的高级策略。当然，大公司也别着<br />
急，周总最厉害的就是各个击破，先解决小的，然后逐个收拾大的。<br />
因此，扣费克星对于用户很有吸引力的。也许周总早就在手机上布局了不少***，然后自己杀<br />
自己。拿着打流氓的旗号，打造一个更大的***公司，和360<br />
在PC上的思路完全一致。<br />
<br />
10. 为什么要以个人名义发布扣费克星而不是360？<br />
很多人会问，扣费克星是个人做的，和360有什么关系呢？360为什么用个人名义发布扣费<br />
克星，估计原因有三：<br />
周总在PC上干得黑事太多，在手机上不想得罪太多人，所以找了个个人作者来顶缸（手机<br />
上的SP公司，如果知道是360挡他们的路，个个都不是好惹<br />
的）；<br />
<br />
用个人作者发布，容易骗取草根网民的认同感和同情心。你看我是一个网民，被扣钱了，<br />
所以做了个软件，帮大家解决问题，外表看起来多感人啊。<br />
用个人作者发布，具有很强的迷惑性，很多SP和无线领域的公司，肯定会认为个人作者的<br />
产品，不会有很大的影响力，所以不太关注。而360已经在暗渡陈<br />
仓，大规模推广扣费克星，等到扣费克星控制局面后，再变身为360。<br />
360官方还象模象样的发表申明说“360只发布了手机卫士（垃圾信息过滤）这一个产品，<br />
其他产品与360无关”，太有点“此地无银三百两”了。360<br />
自己发布了一个进行垃圾信息过滤的产品，从第一个版本看还比较一般，和信安易等比还<br />
有很大距离。当然，360官方说法就是，这是实验室流传出去的<br />
beta产品。据说，这几天又推出了一个安全管家，对软件的评论基本一样，估计也是360<br />
的，手机上即将开始一场腥风血雨。<br />
<br />
11. 奇虎社区今后怎么发展？<br />
答案就是不发展。奇虎的发展，基本完全靠色情擦边球内容来充流量，社区不作了，原有<br />
的人走的走，没有走的，就帮周总做宣传，打口水仗，帮周总打压瑞星、<br />
金山。说到金山，前一段网上有争论“在手机有没有病毒？”，居然有所谓金山的工程师出<br />
来说明没有病毒，不知道他是不是真的金山员工？还是金山前员工？是<br />
代表金山，还是代表个人？也很可能是360雇佣的写手断章取义，扭曲了人家的发言。<br />
我个人认为，手机病毒是事实，这是任何一个安全工程师都知道的基本问题。手机病毒还<br />
没有象PC病毒那样大规模爆发，不像一些手机安全公司宣传得那么严<br />
重，也是事实。当然，善于打擦边球，断章取义，混淆视听是周总及其雇佣枪手擅长的事<br />
情。<br />
<br />
12.为什么周总对媒体控制力强？<br />
周总身边有人上可达政府，下可控制媒体。从3721开始，周总最擅长的就是口水仗，到雅<br />
虎，可以说周总的势力范围更是遍天下，据说为周总服务的记者不下<br />
千人，各地临时调拨的枪手可达万人。奇虎最初要做论坛搜索，Discuzz!和一大批互联网<br />
论坛都是周总投资的。一些媒体朋友不服气，周总甚至设计将其<br />
投入监狱。如此的手段，媒体哪能不服气？<br />
扫黄期间，其他网站都被关了，奇虎没事。但是，最近奇虎常常被点名批评涉黄，前一段<br />
又被处罚了。涉黄这种事情发生多了，渐渐也没有人敢帮了。也许验证了<br />
那句话“夜路走多了，总会见到鬼”。<br />
<br />
13.为什么表面上360如此成功，而最初的核心骨干却一个个都离开了360？<br />
今天360的团队早已不再是360最初的那批团队，最初的核心团队早被周总逐个换掉了，360<br />
产品的方向也偏离了最初的目标。我们（360最初团队）原<br />
本的目标是打造一款伟大的产品，但是360后续发展的方向已经不是我们能够把控的。为<br />
了商业利益，360中加入了太多的、标称为“安全软件”不应该做的<br />
功能。<br />
离开奇虎，实属无奈。我们不愿意自己精心付出心血打造的360产品（最初的360）又一次<br />
成为人人唾弃的***。如果公司真的好，真的是为了用户，我们<br />
为什么要离开？<br />
现在360已经成为周总收黑钱、打压对手的工具，和3761***有过之、无不及，实际上，今天<br />
的360已经成为互联网最大的***，控制了数千万台<br />
PC，比3721最为鼎盛的时候还要大，这是多么可怕的事情。据以前的同事透露，目前360<br />
每天搜集的用户数据量高达TB量级。在360面前，用户毫无<br />
隐私可言，360保险箱甚至直接存放着用户的各类帐号信息。也许，广大网民刚刚逃脱3721<br />
等***的“狼窝”，又进入另一个更大的“虎穴”。<br />
当然，周总常说，流氓足够大了，也就有了江湖地位，就可以漂白了，小流氓是瘪三，大<br />
流氓是黑老大。<br />
<br />
14.为什么要说出这些？为什么要匿名发表？<br />
周总崇尚柔道战略，这是一种小公司冲击大公司，攻击大公司弱点的有效打法，也非常符<br />
合周总的性格。但是，这也决定了周总是个商人，而不是企业家。周总适<br />
合把小企业带到中型企业，却再没有能力打造一个伟大的公司。这一点，从3721，到雅虎<br />
，到奇虎，都已经充分证明了。<br />
周总本人的优点恰恰也是他最大的缺点，缺少承载一个伟大公司的胸怀和气度。了解360<br />
的人都知道，周总身边的人越来越少。当然，周总的用人原则就是，有<br />
利用价值的就用，没有利用价值的就滚；服气的留下，不服气的就滚。这也是我们离开奇<br />
虎的重要原因。我们似乎很难相信，一个曾经的互联网***之父，一个<br />
