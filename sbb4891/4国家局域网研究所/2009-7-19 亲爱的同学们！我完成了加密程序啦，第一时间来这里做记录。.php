下面说说加密证书。<br />
<br />
因为是ΨQ加密，所以只要持有相同的证书，就可以解密出对方加密的内容。证书使用json编码。<br />
<br />
如下是各参数的说明：<br />
pwd　:　加密密码　（根据你喜欢的填写）<br />
s1,s2,hs：　加密标识　（s1,s2不要太长，否则会导致加密后的文字一变得很长）<br />
seed：　加密干扰种子，如果数值是-1，则每次加密时都使用随机干扰数对加密内容进行干扰。如果是不等于-1的值，则使用改值进行干扰。建议使用-1，以让加密结果可以每次不同。<br />
<br />
tf：　json对象，是文本化处理器的配置，下有参数如下：<br />
&nbsp; &nbsp;type:　文本化处理器的类别名称，目前只有default可选，填写其他无效。<br />
