																											<div id="postmessage_673062" class="t_msgfont">这个消息其实已经发布很多天了，当时很想针对此事说点什么，后来新浪将我的博客给彻底封杀了，加上本人工作繁忙，也就没有时间对一些时事发表评论，但这还是值得我们每一个网友高兴的事：网络分析公司StatCounter的数据显示，2009年下半年，Google中国市场份额增长迅速，市场占有率已经上升至43%。而目前其竞争对手百度的市场占有率下降至56%。<br />
<br />
世间还是有公道的，中国人还是分得清善恶是非的，百度在中国一支独大，如今愤怒的网民用实际行动回击了它，谷歌虽然离开，但我们愿意追随，因为谷歌为我们呈现了最真实的搜索结果，而不是像百度那样给我们一个被屏蔽、被检索、被审查的结果。一个公司和一个人一样，关键要看他的目标、导向，如果一个公司是以利益为终极目标，对于社会责任、道德良心它是不会顾及的，而谷歌则恰恰相反，宁可放弃百分之三十份额的中国市场，也不愿意接受中共当局的审查、主动对搜索结果进行筛选。<br />
<br />
有一个深圳的软件工程师Justice前几天给我写了一封信，他说：“……我其实一直沉浸在软件开发中，从来不去关注时事政治，后来中央电视台针对谷歌进行抨击，我就感觉有点不对劲，我们公司的几个同事通过一些网络技术手段才发现，其实这是某些幕后黑手的栽赃陷害，这对我来说真的有很大的冲击。之后他们迫使谷歌离开了中国大陆，我才开始翻墙。翻墙对我来说小事一桩，有时候翻出去也没觉得有什么多大的事，但感觉这是对政府的一种姿态，你不让我们翻墙，我们偏要翻给你看，没事闲着翻着玩呗！我女朋友其实也不愿意翻墙，她觉得国内已经足够了，到后来她发现在腾讯连‘卫生巾’都成为屏蔽的词汇，就感觉太不可思议了，于是，她和她的姐妹们，没事也翻墙了。……翻墙其实是我们每个网民的一项基本技能，如果到现在还有人不会翻墙，那就应该赶紧学习一下才是。被逼翻墙后，我们几个同学都来到推特，想法开始改变了，可以说twitter带给我们这些程序员们的是彻底的颠覆。感谢国家，感谢方滨兴先生，一群埋头苦干的程序员硬生生被他们愚蠢的网络长城逼成了‘网络异议人士’”。通过这位网友的来信我们看到，当局的网络长城造成了适得其反的结果，这个很喜剧，我相信会有很多网友是因为他们的封锁而故意去反封锁，这是为了那种不屈服，人性中的叛逆使然。<br />
<br />
现在互联网上翻墙的方法非常多，我也一直在努力让更多的网友知道，但收效甚微，下面我告诉大家一个非常实用的方法，这个方法其实我已经用了很长时间了，之所以没有拿出来跟大家分享，是担心太多的人知道，可能会导致这个方法也失效，不过现在看暂时他们不敢再对谷歌下手。今天看到消息，谷歌CEO今天要求欧美国家对中国的网络审查一事施压，看得出谷歌没有抛弃中国网民，有很多国际上的组织也在要求谷歌尽量帮助中国人突破网络封锁，所以，既然谷歌有这项功能，我不应该一人独享。下面我跟大家说一下这种突破屏蔽的方法：<br />
<br />
第一步：浏览器地址栏键入<a href="http://www.google.com" target="_blank">www.google.com</a><br />
第二步：点右上角设置――搜索设置，将指令语言改为英语――保存设置；<br />
第三部：保存后谷歌会自动重新载入，进入英文谷歌页面，这时请大家注意右上角的Search settings（搜索设置），然后找到下面的Safe Search Filtering（安全搜索过滤），这里大家会发现有三个选项框，请大家选择Do not filter my search results（不要筛选我的搜索结果），然后点上面的save preferences（保存设置）。<br />
<br />
OK，上面操作完毕之后，您就可以在英文版的谷歌页面上输入任何想要搜索的关键词进行搜索了，这些搜索结果，是绝对没有被过滤的，现在您就可以试一下这些关键词：柳丝（什么数字您应该知道）、江XX与宋XX等……（请注意，这些搜索结果虽然真实呈现出来了，但若您想看内容，仍然需要翻墙才可以。）<br />
