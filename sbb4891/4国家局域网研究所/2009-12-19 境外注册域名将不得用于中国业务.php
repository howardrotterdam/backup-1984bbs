																											<div id="postmessage_382812" class="t_msgfont">最近的一条新闻：境外注册域名将不得用于中国业务（转自<a href="http://www.chinaz.com/Webmaster/report/121Q016232009.html" target="_blank">http://www.chinaz.com/Webmaster/report/121Q016232009.html</a>）<br />
<br />
为规范域名注册管理及服务秩序，有效开展打击手机淫秽色情专项行动，保护广大互联网用户的合法权益，工信部于2009年12月16日组织全国域名注册服务及管理机构在北京召开旨在规范全国域名注册暨贯彻落实依法打击手机淫秽色情专项行动会议。<br />
工信部在依法打击手机淫秽色情专项行动会议上提出从五点对域名进行监管，明确要求域名注册管理机构、域名注册服务机构，要完善管理措施，防止违规网站利用变换 域名等手段逃避监管，即：被关闭网站域名持有者被纳入黑名单进行管理、对进行域名转让并提供他人使用的必须重新注册、对网站未备案的域名不予解析（含跳转）、对相关部门依法认定网站涉黄和违规的域名持有者纳入黑名单予以监管、重点清理域名管理 、服务机构存在问题等五点进行加强监管，全面贯彻落实依法打击手机淫秽色情专项行 动。<br />
目前，从全国域名注册服务及管理工作会议获悉，为了进一步打击淫秽色情的违法网站，国家有关部门将通过一系列的技术手段，对于部份已建站的在境外注册的域名做监管，并对国外的注册商实施相应的管制措施，杜绝非法网站利用境外注册域名境外建站来逃避打击。<br />
此外，会议还讨论了有关境外企业开展中国业务时所使用的网站的相关管理草案，今后将对一些在中国开展业务的外国企业的域名和网站实施监管，这些企业在中国开展业务所用的域名必须通过国内的注册商注册，在境外注册的域名将不得用于开展中国业务。<br />
