													<h2>工信部研究院总工:IPv6革命性道路在中国走不通</h2>
						
						
																											<div id="postmessage_213187" class="t_msgfont"><img src="http://i2.sinaimg.cn/IT/t/2009-08-23/U1418P2T1D3376135F13DT20090823120117.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
工业和信息化部电信研究院总工程师蒋林涛在今天指出，到目前为止，IPv6是能够解决目前地址短缺的问题，但其网络架构并没有发生根本的变化。<br />
<br />
IPv6面临三大难题<br />
<br />
首先是网络安全、可扩展性和服务质量问题。&quot;只要是经营性网络，服务质量都非常重要，但目前IPV6并不能提供电信级的QoS；另外，在网络的可扩展性还需要进一步提高，”<br />
<br />
其次就是与现网的融合，到目前为止，我国普遍采用的是IPV4。存量业务非常庞大，IPv6必须解决与现有业务的过渡问题。这也牵扯到了保护运营商历史投资的问题。<br />
<br />
第三，在标准体系已经基本完成的情况下，中国不可能有IPv6的主导权。“我们目前确实有过渡性话语权，但是不可能有主导性话语权，IPv6仍然是美国主导。”<br />
<br />
革命性道路在中国走不通<br />
<br />
蒋林涛说，业界面临着两条截然不同的路线选择。一种是改良性路线，在原有的基本架构之下修修补补；一种是革命性路线，重新建设一张网络。目前世界上主要走改良性道路。但是美国、日本、韩国提出了革命性路线。<br />
<br />
“革命性的路线关键在于思路对不对，如果思路对，几年之内就可以突破。现在看来走革命性这条路是行不通的，”蒋林涛表示，“我希望能够在这方面做更深层次的论证，能够走一条合理的路，这条路是国际上更有前景的路。” <br />
