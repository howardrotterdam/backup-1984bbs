<br />
这件事是这样的，菜头说的东西很对，但是菜头误解了我的意思。为了避免口水战，我只会陈述事实，不会添加任何观点。<br />
<br />
1. 菜头肯定也知道，GFW有4中封锁方式，IP封锁是最恶劣的一种。<br />
2. HOSTS方法恶劣之处就是直接暴露了网站的IP地址，北京的工作人员都不用fuzzing和搜集了。<br />
3. 我不是不推广翻墙方法，而是绝对要回避公开传播HOSTS这种。我曾经是最早各大论坛科普GFW的那一代人，也是最早推广ssh -D的那一批用户（Baidu/g.cn搜索ssh 代理）<br />
4. 为什么我反对HOSTS，youtube前一次封锁的时候，豆瓣的google小组已经有人发了，访问<a href="http://xxx.xxx.xxx.xxx" target="_blank">http://xxx.xxx.xxx.xxx</a> （youtube的IP） 就可以打开youtube，结果上午还可以用下午就不行了。。。那个帖子后来大家得出一致结论，域名被封，IP没封，你自个知道就行了，不要把IP公开出来。<br />
5. 远在菜头写hosts方法之前，我在twitter和douban里动过私信的方式至少告诉过50个人这种方法。我想我已经足够推广这种方法了。但是我传播的前提是我个人可以相信（通过他们的网上据点和活动可以判断出）他们都是有credibility的网友。<br />
6. 我所说的“hosts帖子虽然能给你博客/论坛带来流量”肯定不是单独指菜头您的博客，因为您的博客无论有没有那个帖子已经够牛逼了 :)<br />
