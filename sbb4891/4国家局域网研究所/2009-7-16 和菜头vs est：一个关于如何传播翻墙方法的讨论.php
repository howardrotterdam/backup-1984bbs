之前发布过一篇教大家如何访问Youtube的帖子。此后，我在网上看到了提供修改方法的Est所写的博文：《Hosts不是万能的》。文章中说到：<br />
<br />
hosts这种比较珍贵的方法<br />
私下互相信任的好友之间交流即可<br />
hosts帖子虽然能给你博客/论坛带来流量<br />
实际上是损害了其他所有人和站点的利益。<br />
IPv4地址有限请多珍惜<br />
<br />
事实印证了Est的话，所以，他又写了一篇帖子《youtube，GoogleVideo完全歇菜》。<br />
<br />
从两个帖子以及帖子的回复上看，Est是位技术人员。在我刚开始上网的时代里，技术人员是网民的天然同盟。你做不到的事情，他们在BBS里写出方案。你访问不了的站点，他们会设计工具帮你访问。现在的情况不一样了，技术人员把无法访问Youtube的责任归罪于类似我这样发布Hosts地址的人，认为这样的举动“损害了其它所有人和站点的利益”。事实也站在他的一边，目前已经无法再用原来的方法访问Youtube了。<br />
<br />
关于这件事，我的个人想法是这样的：技术和知识成为某一部分人的特权或者利益，我无法认同这一点，尤其是在网上。如果访问Youtube的方法只能为部分人所有，那么不如不要。事实上，我不认为修改Hosts文件的方法对于技术人员来说意义重大，非此不可。连我这样对技术一窍不通的人，也能用其它多种方式继续访问Youtube、Facebook或者Twitter。相信技术人员会有更多的方式方法访问，其中一个方法失效，没有限制在小圈子内使用，失去了特别的权力，就因此指责别人，我理解不了。<br />
<br />
就像一群无辜的人被囚禁，其中有几个人发现了一扇可以呼吸新鲜空气的窗子。由于消息走漏，窗子前围的人太多，造成狱方用砖头封闭了窗子，于是，这几个人就开始指责大家，指责传递消息的人，我觉得这大概不是很公平。与其指责狱友，不如教他们怎么挖洞。囚禁这件事情本身不对，不去讨论这件事情，解决这件事情，而是在这个框架下开始讨论利益问题，那么，我以为这样的结果只有让部分人当上牢头狱霸，独占一扇窗子作为工资，才能了结。<br />
