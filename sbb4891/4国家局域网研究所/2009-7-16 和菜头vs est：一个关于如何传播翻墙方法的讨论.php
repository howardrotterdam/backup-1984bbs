<br />
改Hosts文件是最可控，最直接，最安全的方法。<br />
<br />
您是在说反话么？hosts最不可控（DNS record类型不可控），最不直接（最直接的是修改协议栈直接忽略所有RST包，一劳永逸），最不安全（最容易被封）的方法。<br />
