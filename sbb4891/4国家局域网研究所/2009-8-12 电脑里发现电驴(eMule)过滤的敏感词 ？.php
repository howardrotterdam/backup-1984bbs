不知此话题是不是有人发过，不过昨晚看到后还是吓了一跳....我用的eMule是从国外站点下的，因此目前搜索整个电脑，尚没有这个wordfilter.txt，情绪稳定。莫不成eMule也像Skype那样把国人给卖了？<br />
<br />
电脑里发现电驴(eMule)过滤的敏感词<br />
【福禄祯祥7月8日文】晚上使用Google Desktop搜索电脑硬盘里的“Hu Jintao+西藏”时，找到一个名为wordfilter.txt的文件，打开一看，竟全是所谓的敏感词，色情与政治同在。该文件位于eMule（电驴）的子文件夹config里。<br />
<br />
eMule是一个P2P下载软件，由国内的VeryCD网站推广。我的电脑的C盘和D盘里都分别安装了eMule，结果这个两个文件夹的里都有wordfilter.txt文件。<br />
<br />
所谓的政治和色情敏感词按照汉语拼音顺序排列，两者混杂在一起。除了中文简体，还有汉语拼音和繁体的。除了正经八百的敏感词，还有其谐音或与之相近或相似的字眼，甚至连XXX也包括在内。<br />
<br />
电脑显示D盘的eMule文件夹创建时间是2007年5月7日, 11:35:31，这个应是我安装eMule程序的时间。其中的wordfilter.txt文件的创建时间是2006年10月13日16:07:12，这个应是wordfilter.txt置入eMule程序的时间。也就是说，这个wordfilter.txt是在我安装eMule时，一同进入我电脑硬盘的。<br />
<br />
我一直使用的是安装在C盘的eMule程序。电脑显示C盘的eMule文件夹的创建时间是2007年5月12日, 14:11:02，其中的wordfilter.txt文件的创建时间是2008年8月27日, 18:14:16。同样道理，这就意味着，这个wordfilter.txt是在我安装eMule之后一年多才进入我电脑硬盘的。而eMule有自动更新的功能。如此看来，C盘的wordfilter.txt很可能曾被eMule自动更新过。<br />
<br />
与D盘的wordfilter.txt不同，C盘里的wordfilter.txt显示的不是一目了然的敏感词，而是一大堆阿拉伯数字和英文字母及加减号。如果不是乱码，难道是敏感词以密码的形式出现了？<br />
<br />
中国当局试图强制推广的过滤软件“绿坝”，因其担负着不可告人的政治任务和技术方面的漏洞百出，而饱受讨伐。但是现在看来，早有程序单独履行“滤坝”的职责了，并且已经潜入电脑用户终端了，因为它是随风潜入夜式的神不知鬼不觉地就被安装到电脑上的，因此知道的人不多，自然也就免缺了“绿坝”所遭受的批评和阻力了。<br />
<br />
更可怕的是，我们不知道还有哪些软件也已经在电脑终端执行过滤任务了，过滤的又是哪些内容。<br />
<br />
希望能开发一种类似防病毒软件那样的防过滤软件，用于检测和清除已经装入电脑的过滤程序，并对试图入侵电脑的过滤程序进行预警或拦截。你有“滤坝”，我有“反滤坝”，看谁怕谁。（文/福禄祯祥<a href="http://fulue.com/" target="_blank">http://fulue.com/</a>）<br />
