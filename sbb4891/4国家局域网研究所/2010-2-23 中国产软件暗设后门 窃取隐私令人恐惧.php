中国多款杀毒软件产品安有“后门”，令人恐惧（自由亚洲电台） <br />
<br />
中国产软件暗设后门 窃取隐私令人恐惧<br />
 <br />
 【大叽歪2月23日讯】中国多款杀毒软件产品被发现非法盗取用户个人资料，杀毒软件公司甚至胁迫网站站长加入其联盟。专家认为，由于官方监管不力，甚至纵容或者合谋，不少用户对国产软件感到恐惧。<br />
<br />
据自由亚洲电台报导，近期中国网络上出现大量讨论国产杀毒软件的内部存在盗窃信息的“后门”的情况，有用户专门分析了几款杀毒软件，分析结果让人不寒而栗。<br />
<br />
360安全卫士：每五分钟窃取一次用户隐私信息<br />
<br />
分析发现，自称有2.6亿用户的杀毒软件360安全卫士存在非法盗取用户信息的后门。这款软件在安装进用户电脑时，会偷偷设置一些和杀毒无关的功能，然后窃取用户的个人资料。著名博客作者郭卫东星期一告诉自由亚洲电台记者，“360的问题已经由网络资深技术人员发现每隔五分钟把用户电脑上使用的隐私上传到独立的IP地址上，这种做法肯定是违法的” 。<br />
<br />
用户说，由于360卫士偷偷上传用户私人信息，占用了极大的资源，所以会造成上网变慢。但是由于盗窃信息的“后门”运行在系统最底层，用户很难发现。今年2月，专家在检查360安全卫士的漏洞时，才偶然发现其中的盗窃信息功能。<br />
<br />
瑞星杀毒软件：胁迫网站加入其联盟<br />
<br />
羊城网的站长小劳专门写信给知名博客作者阮一峰，揭示了另一款软件“瑞星杀毒软件”故意对羊城网的一些页面报告有病毒，然后胁迫用户加入其“瑞星安全网站联盟”，但是实际上这个网页并没有问题。<br />
<br />
瑞星公司曾贿赂北京市公安局网监处处长于兵，陷害竞争对手微点公司。2005年7月5日，微点公司莫名其妙地被网监处调查。网监处先是以“反病毒公司资质调查”为由，后又以“未采取安全防范措施”的案件调查为名，对微点公司进行了一个多月的“检查”，频繁传唤包括专家刘旭在内的公司管理、研发人员。同年7月21日，办案人员将扣押的装有微点核心技术的计算机送到瑞星公司。<br />
<br />
目的：配合政府监控网民或非法竞争<br />
<br />
至于杀毒软件公司为何要盗取个人用户信息，郭卫东分析表示，“窃取用户隐私，在用户不知道的情况下设置一些木马，或者后门程序，来获得一些商业上的信息；还有一个就是和国家安全部门合作，网络监控部门可能会通过杀毒软件公司提供的信息找到发布文字的人” 。<br />
<br />
专家认为，一旦用户都是在不知情的情况下，安装了以为是帮助杀毒的软件，实际上是把自己暴露在被监控的危险的情况下。商家可以通过植入的后门，远程控制亿万用户的电脑。再利用这些电脑去攻击其它对手、堵塞网络等。同时黑客也会利用这个后门，从事多种不法活动。<br />
<br />
资深网络评论员洪波对此表示，“这也算是一个竞争策略吧，很多用户都会关心自己的上网安全问题，安全就会成为一种胁迫手段。（这些软件）不是为了真正解决安全问题，更多地是为了解决市场竞争的问题，把安全当作一种胁迫手段要挟用户，要挟网站，这是不正当的”。<br />
<br />
对于瑞星公司陷害微点公司，阮一峰表示，这种公司的商业诚信，是令人质疑的。洪波认为，商业公司存在非法竞争手段，一部份是出于官方监管不力甚至是纵容或合谋的结果。去年中国国家工信部主导的“绿坝”软件，也存在诸多漏洞和安全隐患，用户被胁迫使用号称“安全”的软件，实际上是陷入更加危险的境地。</div>

							
							
							
							
