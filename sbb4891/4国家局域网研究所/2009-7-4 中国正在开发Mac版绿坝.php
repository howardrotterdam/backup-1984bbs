Days later, several U.S. technology advocacy groups banded together, asking the Chinese government to reconsider its mandate and “welcoming the opportunity for meaningful dialogue” about parental control software. <br />
 <br />
However, it was after researchers at the University of Michigan revealed that the software not only had a cache of terms it blocks users from ― that beyond pornography included mentions of Falun Gong and homosexuality ― but had serious security flaws, China’s Ministry of Industry and Information Technology told AP that the software was “not compulsory.”<br />
 <br />
On June 30, just under the mandate deadline, the Ministry reportedly announced that “for practical factors including heavy workload” companies may delay pre-installing the software.&nbsp;&nbsp;<br />
 <br />
Before celebrating could too heavily get underway, on July 2 the Ministry told China Daily that the mandate had only been delayed, not cancelled, and that “The government will definitely carry on the directive on Green Dam,” according to a report from Information Week.<br />
 <br />
When the Wall Street Journal first broke the story in early June, Green Dam was described as having been developed by Jinhui Computer System Engineering, with input from Beijing Dazheng Human Language Technology Academy, and compatible with the Microsoft Windows operating system. This left Apple quietly on the sidelines, while HP, Lenovo, Dell and others wondered how to proceed. <br />
 <br />
A sales representative at the Beijing Apple store said Green Dam wasn’t being bundled with new machines, since there was no Mac version, but that when one becomes available ― which Jinhui says it is testing― then the Apple store would “pre-install the program in accordance with the government mandate,” the sales rep said, according to a July 2 news from PC World.<br />
