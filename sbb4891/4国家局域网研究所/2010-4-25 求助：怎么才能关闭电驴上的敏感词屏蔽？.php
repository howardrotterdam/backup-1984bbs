<br />
　　禁止从PeerCache中下载：不用设置。<br />
<br />
　　建立新的Part为稀疏文件：只有NTFS格式下才能使用（Windows 7 因为官方认为其稀疏文件系统有问题，所以不能开启）。<br />
<br />
　　关于稀疏文件的解释：稀疏文件就是在文件中留有很多空余空间，留备将来插入数据使用。如果这些空余空间被ASCII码的NULL字符占据，并且这些空间相当大，那么，这个文件就被称为稀疏文件，而且，并不分配相应的磁盘块。<br />
<br />
　　为非稀疏文件分配磁盘空间：最好不要设置，因为eMule会自动随着下载而进行磁盘空间的申请操作，如果你怕造成过多的磁盘碎片的话，你就开启这个选项吧。<br />
<br />
　　检查磁盘空间：你害怕你的空间不够吗？开启这个后能自检空间的剩余情况，并作出通知，前提是你开启了磁盘不足的警告通知 <br />
