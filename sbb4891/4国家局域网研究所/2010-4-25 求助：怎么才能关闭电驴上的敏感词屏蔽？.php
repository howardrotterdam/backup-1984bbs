																											<div id="postmessage_584297" class="t_msgfont"><strong>以下援引维基 </strong> ：<a href="http://zh.wikipedia.org/zh-cn/Emule" target="_blank">http://zh.wikipedia.org/zh-cn/Emule</a><br />
<br />
　　eMule是一个开源免费的P2P文件共享软件，基于eDonkey2000的eDonkey网络，遵循GNU通用公共许可证协议发布，运行于Windows下。eMule中文名称在大陆用户中有争议，根据eMule官方网站的简体中文版说法，“eMule”译为“电骡”，其名称也来源于动物：骡[1]。<br />
<br />
　　eMule于2002年5月开始开发。与之前的eDonkey2000客户端相比，eMule能够连接eDonkey和Kad两个网络，有较快的下载损坏数据恢复功能，有奖励频繁上传的用户的积分系统。另外，eMule以zlib压缩格式传输数据以节约带宽。<br />
<br />
　　eMule用Microsoft Visual C++编译，使用了MFC。由于eMule开放源代码，其代码基础也被Linux平台下的客户端xMule和跨平台客户端aMule、JMule所使用。同时eMule也派生出了很多修改版，即eMule Mod(s)。<br />
<br />
　　很长时间以来eMule都是SourceForge网站上的下载量最多的软件。截止2009年9月，官方eMule在SourceForge上的下载点击数已超过5亿[2]。<br />
<br />
<strong>以下援引悼芹轩</strong> ： <a href="http://www.xiaoyaoblog.cn/" target="_blank">http://www.xiaoyaoblog.cn/</a><br />
<br />
　　<strong>无耻的vc ws地埋人</strong><br />
<br />
　　VC者，VeryCD也。以引进、汉化、修改国外的优秀分享软件emule（电骡）起家，在众多网友的支持下，现在颇具规模。然而，vc的发展，却也渐渐背离了最初的理念，比如停掉bbs、私下达成商业协议放行吸血骡、误导国内的新手等行径，无疑是为了尽快掏钱，而置整个ed2k的生态环境于不顾。<br />
<br />
　　其中，放行吸血骡这一条尤为可恨。国内众多的小白，以及众多的伸手党，本已为吸血骡提供了丰沃的土壤。这本需要所有的骡友意识到其危害，积极地反吸血。可vc却迟迟没有反应。后来在广大网友的一再要求下，才使其开发的mod支持了dlp。然而，虽然支持了dlp功能，但vc自己修改的dlp插件，却对国内最广泛的吸血骡――迅雷偷偷放行，只屏蔽掉了非常古老的那一部分。而其原因，则是vc和三代达成了某种商业上的协议。<br />
<br />
　　前两天，vc上有一个用户，不堪吸血骡之扰，在vc的社区发了个帖子，说希望官版的emule能加入dlp――他以为dlp是vc开发的，故而希望vc向官方提供dlp，推动官方支持dlp。这本不过是个很平常的帖子，我也只是随便回复了一下，说明了官方对dlp的态度，dlp的来历，以及vc对xl暧昧的态度。本以为就到此结束了。不料vc的版主认为我所说的vc版dlp是阉割版不合实事，而坚持声称vc的dlp效果很好。于是我便与之争执起来，也有几个老骡友仗义助拳，揭露vc种种无耻的做法。当然vc的版主不以为然，双方争执不下。后来又涌进几只无知却又自大的小白，于是战况更为激烈。<br />
<br />
　　至昨天晚上，我想斗嘴也不是办法，还容易留下“无凭无据、信口雌黄”的印象。于是干脆心一横，上了大杀器――截图！我将verycd的阉割骡easymule加载其自产阉割dlp，放行xl的情况，通过日志、截图，予以充分的说明。上传完毕，睡觉。当时心里就基本肯定，这些图绝对会删掉；说不定，我的号也像当年aug那样，说出事实，却被vc以“造谣”为名，出手格杀。――当然，aug当时说的事实，要严重的多，乃是vc的dlp，一边放行冒充优秀mod xtreme的xl，一边却封禁了原版的xtreme7.0！<br />
<br />
