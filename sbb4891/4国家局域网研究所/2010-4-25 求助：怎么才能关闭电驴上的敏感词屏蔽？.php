　　限制快速连接：这个是限制双方连接的，太快的连接可能会导致网络设备的瞬间无响应，所以你确认你的网络设备能够很好工作在快速响应模式下。 服务器连接刷新间隔：建议大家控制大点的时间，否则刷新好像没有什么效果。像我基本上就没有效果 仅在运行是接受eD2k链接：如果你想关掉eMule的情况下，也能响应文件链接，你就关闭吧。这个选项关闭后，以后你只要点击了eD2k链接，不论eMule是否已经运行，eMule都会弹出来下载的。<br />
<br />
　　启用信用系统：这个对那些上传多的人是有帮助的。请注意，不打开的话，你的积分是在别人的电脑上面减半。<br />
<br />
　　在启动/退出时控制windows的防火墙：自动打开相应的端口，退出时自动关闭，好像我不用Windows防火墙的人来说，这个选项毫无意义。<br />
<br />
　　过滤内网服务器和用户：开启意义不大，因为内网IP在公网上无法解析，另外，所谓的内网互联并不受此影响，当前的内网互联并不是通过一个虚拟的内网实现的。<br />
显示更多控制：如果你的 eMule Xtreme 界面和我不同的话，是因为我开启了这个选项！<br />
<br />
　　禁止在文件详情里自动开始预览压缩文档内容：eMule可以读取ZIP格式的文档内容，建议不要关闭，这样在下载一些ZIP压缩包的时候就知道里面有哪些东西了。<br />
