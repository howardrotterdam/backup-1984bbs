<br />
有邀请码的人先送一些邀请码出来，然后大家按回帖顺序排队领取，不过领完的人要记得等有了邀请码的时候，再来这里赠送至少1个，这样邀请码的数量就不会减少，而是越来越多，而且大家按回帖顺序领取，也不用抢<br />
