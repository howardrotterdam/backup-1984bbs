<strong>求邀请码</strong><br />
然后等收到别人用论坛短消息发给你的邀请码之后，改帖子为：<br />
<strong>收到1个邀请</strong><br />
然后等你也有邀请码的时候，请用论坛短消息发给下面最靠前的求邀请码的人，然后改帖子为：<br />
<strong>收到1个邀请<br />
1个邀请已赠送x楼</strong><br />
<br />
如果出现有多个人给你短消息发邀请，请自觉把多出来的转赠下面的人，并记得修改帖子，如：<br />
<strong>收到3个邀请<br />
2个邀请已赠送x楼，x+1楼</strong><br />
然后等你自己也能邀请了，再回来至少回赠一个，并把帖子改为：<br />
<strong>收到3个邀请<br />
3个邀请已赠送x楼，x+1楼，x+2楼</strong><br />
<br />
<br />
我这里有3个邀请，准备送3,4,5楼的同学<br />
