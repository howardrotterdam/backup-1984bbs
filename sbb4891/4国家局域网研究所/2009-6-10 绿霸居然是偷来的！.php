是判断流氓软件的两条准则，而这个绿坝完美得全部符合。<br />
<br />
　　4000 万，4000万纳税人的血汗钱就换来了这么一个流氓软件。它的存在真<br />
的是为了保护未成人收到色情网站的毒害吗？未必，论网站过滤，9几年的美萍<br />
做的比它要好。甚至不用付出任何费用。监视大量应用程序，定时对系统进行截<br />
