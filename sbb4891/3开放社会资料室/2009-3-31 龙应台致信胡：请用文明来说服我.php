如何面对被侮辱和被损害的状况 <br />
<br />
　　出现这些现象与中国长期处于被侮辱和被损害的境遇息息相关。面对如是现实，可以有不同的心态。 <br />
　　西方的入侵彻底改变了中国历史的行程。伴之而生的是天朝大国的表象破裂，大量民众在生死线上挣扎。人们顺理成章把这种状况归罪于“洋鬼子”；也谴责统治者腐朽、愚昧、软弱。一个辩论不休难于取得共识的问题是：内因还是外因是导致这个状况的主要根源？ <br />
　　其实，完全可以从另外一个角度提出问题：这个状况迟迟不能改变的原因何在？如果有人说这是因为帝国主义者太凶狠了，这等于什么都没有说。经过长期、复杂、反复的博弈过程，在国际关系中可以逐步建立比较合乎多数人和多数国家长远利益的“正义”秩序。当这个状况尚未出现以前，不会有救世主从天而降，慷慨代你维护国家利益。问题只能归结为面对这样的现实，如何才能走出困境？ <br />
　　海内外的经验证明：后发展国家和地区(殖民地、半殖民地)改变不发达状况，改变被动局面的惟一道路，是向西方列强学习，实现社会生活的全面现代化。成败的关键在国内的改革。这是一个社会运行机制的全面改造过程。对那些文化自成体系，而对外来文化深闭固拒的国家说来，这是十分艰难的过程。以中国来说，从鸦片战争算起至20世纪初实行新政，仅是辩论要不要改革就整整花掉60年！至于改革取向，包括是通过革命手段还是通过渐进的改革开辟前进道路，更是头绪繁复。不过，有一条是肯定无疑的：必须千方百计争取一个和平的国际环境，为国内的改革和建设赢得充分的时间。如果此说大致不差，回头再看义和团，对内，它是与社会前进方向背道而驰的反动事件。对外，乱杀洋人不但是反人道、反文明的罪行，也是极端愚蠢危害中国自身利益的暴行。 <br />
　　有个流行多年为义和团事件辩护的论断：义和团避免了中国被瓜分。早在1989年已故历史学家李时岳先生已经详尽地驳斥了这一诡辩。不但4亿5千万两赔款(相当于当时将近6年的全国财政收入)像一支巨大的吸血管插进中国人的胸膛，而且给沙俄藉口，趁机制造了海兰泡和江东64屯惨案，7000多中国人被杀，江东领土全被吞没，大量俄军进占东北；华北地区在战争中死伤烧杀的损失难以数计。战后的瓜分阴谋更没有停止：英军进攻西藏，占领拉萨；德国派炮舰进入洞庭湖，并要求租借洞庭湖和鄱阳湖沿岸；英国则相应要求租借舟山群岛作为“补偿”！ <br />
　　有人喜欢援引八国联军统帅、德国人瓦德西的这么一段话：“无论欧美日本各国，皆无此脑力与兵力可以统治此天下生灵四分之一，故瓜分一事，实为下策。”证明义和团化解了瓜分图谋。李时岳先生说得好：“瓦德西个人的观感并不能代表德国的政策，德皇一直把瓜分作为对华政策的基点，上述要求‘租借’洞庭湖和鄱阳湖沿岸的行动就是证明。只是由于帝国主义之间的矛盾，瓜分才没有实行。” <br />
　　把视野放得更宽一些，问题就更加清楚。前人早已指出：甲午战争、戊戌变法和义和团事件是一条割不断的历史链条。说得更准确一些是：甲午战争彻底暴露了大清帝国的腐朽，不少知识阶层从几十年迷梦中惊醒，反思自强运动不敢触及“自由不自由”这个根本问题的错误，形成了第一次群众性启蒙运动，改革也有新进展。是学习西方彻底改革，还是固守传统，不准变革，成了中国盛衰的关键，也是解读这段历史的基本线索。不幸，体现甲午战败后的变革进程进入高潮的戊戌变法以失败告终。戊戌政变标志着学西方、求变革的挫折和倒退；义和团事件不过是政变后固守传统反对变革的反动逆流的巅峰。换句话说，义和团事件对外使中国在被奴役的附属国的道路上继续沉沦，对内则举目皆是国破家亡的图景。 <br />
<br />
走出把革命粗鄙化的文化心态 <br />
<br />
　　2000～2001年之间，引起中国人关注的一个国际事件，是日本的教科书问题。一部右翼势力编纂的历史教科书掩盖历史真相，否认日本政府犯下的侵略罪行，激起包括中韩两国政府和人民在内的海内外朝野人士强烈抗议。这是伸张正义的斗争，而且这是20年间第四次了。1982、1986、1996年都曾出现新修教科书歪曲历史，一再在日本国内外激起公愤。这一 日本思想文化领域的顽症，促使许多人形成一个极为深刻的印象：日本人缺乏忏悔意识。人们还进一步追问：为什么会出现这样死不认罪的现象？这是不是大和民族特有的缺陷？ <br />
　　看看上述中国的教科书问题，一个合理的推断是，我们的近代史观也有类似的问题。当然日本是侵略者，中国是被侵略者，这是截然不同的。可是，两者也有共同点：社会的主流文化都对自己的近代史缺乏深刻的反思。 <br />
　　从20世纪初开始，中国的有识之士一再提出要改造中国人的“国民性”。这些先驱用心良苦，但他们没有进一步追问：决定国民性的主要因素是什么？可以说，国民性是一国公民思维和行为方式的特点。任何民族都是从吃人生番演变过来的。作为一个群体，文明程度的高低和野蛮孑遗的大小，决定性的因素是受文化传统和制度制约的自我净化能力的强弱。 <br />
　　被侮辱被损害的屈辱，给中国人构筑了新的思想定势。这突出地表现在长期以来形成的一个似是而非的观念：因为“洋鬼子”是侵略者，中国人怎么做都是有理，都应歌颂。这是爱国主义的要求。 <br />
　　现在的历史教科书就是以此为指导思想的。热爱自己的祖国，理所当然。可是，如何爱国，却有两种不同的选择。一种是盲目煽动民族情绪；中国传统文化中“严华夷之辨”、“非我族类，其心必异”等观念已经深入骨髓。时至今日，余毒未清。新的版本是：中外矛盾，中国必对；反列强、反洋人就是爱国。在史料选择和运用中，不管是真是假，有利中国的就用。另一种选择是：以理性的态度分析一切；是其是，非其非，冷静、客观、全面地看待和处理一切涉外矛盾。 <br />
　　现代化的基本精神就是理性化。如果我们认同这个基本观点，就应该引导中国人往这条道上走，让理性、宽容内在化，成为中国人的国民性，以利各国人民和各种文化和谐共处。在全球化迅猛发展的时代，企业之间和国家之间的利益冲突不可能泯灭；理性地认识和化解矛盾对任何国家和企业都是最好的选择。如果一涉外就是“反帝”、“反霸”，非把事情弄砸不可。 <br />
　　例如，法是人类文明的结晶，社会运行的规则。国际条约是有法律效力的。人们可以指责这些规则和条约是列强主导下形成的，不利于弱国和贫苦民众。人们应该不断批判和揭露它的谬误，通过各种力量的博弈，形成新的规则，修订新的条约。可是，在没有修改以前，我们仍然不得不遵守它，否则就会造成不应有的混乱，归根到底不利于弱国和多数民众。 <br />
<br />
　　19、20世纪中国人干了不少“无法无天”的事，义和团事件是其中的典型。值得重视的是不但至今有人把野蛮的行为说成是“革命”，而且到了20世纪90年代，有人竟把主张遵守现行国际条约的观点视为应该严加批判的卖国投降观点！ <br />
<br />
　　说到底，这是把革命粗鄙化的流毒。 <br />
<br />
　　必须清醒地看到，在社会领域，只有引发制度变革的行动，才称得上真正的革命。太平天国和义和团都不符合这个要求。这样的歪曲实际是把革命粗鄙化，迟早总要付出代价。 <br />
<br />
　　不能轻视这些错误教育的后果。违反常识理性，以“革命”的名义故意歪曲历史真相，歌颂义和团的直接恶果在“文化大革命”中就暴露无遗。红卫兵火烧英国代办处，是义和团行动的翻版；“破四旧”和“反帝”、“反修”中体现的清除外来事物的疯狂，这些行动体现的内在理路，也与义和团的“灭洋”如出一辙。 <br />
<br />
　　上述教科书的编写所呈现的理路，也没有什么不同。它们的共同点是：1.现有的中华文化至高无上。2.外来文化的邪恶，侵蚀了现有文化的纯洁。3.应该或可以用政权或暴民专制的暴力去清除思想文化领域的邪恶。用这样的理路潜移默化我们的孩子，不管主观意图如何，都是不可宽宥的戕害。 <br />
<br />
　　为了培育理性的有法治观念的现代公民，以利于现代化事业，现在是纠正这些谬误的时候了。</div>

							
							
							
							
