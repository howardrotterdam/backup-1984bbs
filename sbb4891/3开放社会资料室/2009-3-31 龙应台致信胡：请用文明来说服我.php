　　对义和团事件和八国联军评述比较全面的同样是香港的教科书。它既谴责义和团“大肆排外，杀教士、教民，连藏洋书、戴眼镜的人都不放过，且到处破坏，烧教堂、拆电线、毁铁路。”“日本使馆书记杉山彬、德国公使克林德先后被杀”；也指出“当时联军纪律极坏，任意焚掠屠杀，其中以俄、德两国军队及英国的印度兵最为残暴。”细致分析了义和团产生的背景：1.民族情绪。2.民生困苦。3.列强侵略。4.教案频生。还全面论述了辛丑条约的内容及它对当时和日后中国的深远影响。任何不抱偏见的人都会承认，这部教科书说的是真实的历史。<br />
