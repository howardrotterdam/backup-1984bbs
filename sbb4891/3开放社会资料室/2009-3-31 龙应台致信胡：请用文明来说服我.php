																											<div id="postmessage_65744" class="t_msgfont">作者：袁伟时(中山大学教授)<br />
<br />
　　21世纪的中国人，面对的是顺之者昌、逆之者困的全球化趋势。与此同时，中国的现代化事业进入了关键时刻。在这个年代，决定公民和国家发展成败的最重要条件是制度环境，但公民的心智状态对自己乃至国家和社会发展的影响也十分巨大。 <br />
　　20世纪70年代末，在经历了反右派、大跃进和文化大革命三大灾难后，人们沉痛地发觉，这些灾难的根源之一是：“我们是吃狼奶长大的。”20多年过去了，偶然翻阅一下我们的中学历史教科书，令我大吃一惊的是：我们的青少年还在继续吃狼奶！ <br />
　　“以史为鉴”、“前事不忘，后事之师”，这是中国人耳熟能详的名言。屈辱、挫折、兵连祸结、前仆后继，一部中国近代史蕴藏着多少血泪和经验教训！我们有责任将历史真实告诉我们的青少年，让他们永志不忘。这是帮助他们成为现代公民的必由之路。如果天真纯洁的孩子吞食的竟是变味乃至有意无意假造的丸丹，只能让偏见伴随终生，甚至因而误入歧途。 <br />
　　现在是正视我们自己的历史教科书问题的时候了。现从几个具体的历史事件谈起。 <br />
<br />
　　火烧圆明园是不是无法避免的？ <br />
<br />
　　火烧圆明园是英法侵略军犯下的不可饶恕的罪行。事情为什么会弄到如此地步？140多年过去了，我们理应冷静地考察双方的应对得失，吸取教训，让各国人民更好地共处。这一事件是“第二次鸦片战争”的恶果之一。人民教育出版社历史室编著的《中国历史》第三册，是被普遍采用的九年义务教育三年制初级中学教科书。它是这样评述这次战争的： <br />
<br />
　　一、关于战争起因。 <br />
<br />
　　这部教科书写道：“1856年3月，法国天主教神甫马赖，潜入广西西林地区胡作非为，被当地官吏处死。这就是所谓的‘马神甫事件’。后来，法国以此为借口，伙同英国发动侵略战争。同年10月，广州水师在中国商船‘亚罗号’上，缉捕了海盗和水手。英国领事无端干涉，硬说‘亚罗号’是英国船要求中国方面释放被捕的人，并向英方赔礼道歉。两广总督叶名琛怕事态扩大，释放了被捕的水手，但拒绝道歉。这就是所谓的‘亚罗号事件’。1856年10月，英国首先挑起战争，炮轰广州，第二次鸦片战争开始。” <br />
<br />
　　这里说的亚罗号事件大体符合历史事实。至于杀法国天主教神甫马赖(Auguste Chapdelaine)，至今仍是一笔糊涂账。马氏是1856年2月29日被广西西林代理知县张鸣凤所杀的。直至法国公使查问，张鸣凤仍然矢口否认，说根本没有这回事。致使广西按察使和两广总督到了1858年初还信以为真，据此回答法国公使和上奏朝廷。 <br />
　　1844年10月订立的中法《黄埔条约》规定，法国人只准在五口通商的双方“议定界址内”活动，“法兰西无论何人，如有犯此例禁，或越界，或远入内地，听凭中国官查拿，但应解送近口法兰西领事官收管；中国官民均不得殴打、伤害、虐待所获法兰西人，以伤两国和好。” <br />
　　马氏1842年起，便到西林传教，《黄埔条约》订立后仍不离开，这是违反条约的错误行为。但把他处死，显然是西林地方官员的行为，违反了应把拘捕的法国人解送领事的条约义务。直至现在人们仍无法确定马氏确有该处死刑的哪些罪。按照程序正义优先的法学观点，中方无疑理亏。教科书对此事的评述是不准确的。 <br />
　　还要指出，教科书只字不提引发这次战争的两条根本原因：一是英国政府要求清政府忠实履行《江宁条约》的规定，其中重要一点是让英国官员和商人可以自由进入广州城。让洋人进城，现在看来，完全是不值一提的小事，当时在五口通商的其他四口尽管也有过大小不一的纠纷，但都一一化解，没有酿成巨祸。惟独在广州，却惊动朝野上下，闹得天翻地覆，开各地反入城斗争的先河，历时十多年无法解决，直至兵戎相见。 <br />
　　二是《望厦条约》规定：“所有贸易及海关各款恐不无稍有变通之处，应俟十二年后，两国派员公平酌办。”《黄埔条约》亦规定：“若有应行更易章程条款之处……核计满十二年之数，方可与中国再行筹议。”修改有关的通商条款，本属平常外交事务，清政府也一再拖延，加深了双方的矛盾。 <br />
　　关于挑起这次战争的原因，当时的有识之士就有所反思。深悉内情的薛福成沉痛地说：“英人初志在得入城见大吏，借以通隔阂、驭商民，乃粤民一激再激，叶相(叶名琛)复一误再误，使拱手而有粤城……益知中国易与，遂纠法、俄、美三国兵船北上，驶入大沽，阻我海运，立约而还……粤民激于前此大府议和之愤，万众一辞，牢不可破，必阻其入城一事以为快，屡请屡拒，纷纭者二十年，而大沽之失，天津之约，皆成于此，由今观之，甚无谓也。”晚清曾国藩、李鸿章、冯桂芬、郭嵩焘等人屡以“勿以小嫌酿大衅”相告诫，其中就包含了以广州反入城斗争为开端的惨痛教训。 <br />
　　同属中华人民共和国，香港的中学历史教科书就比大陆编得高明。它把这次战争的起因归结为四点：1.外人入城问题。2.续修条约问题。3.阿罗号船事件。4.马赖神父事件。这样说符合历史实际，无损中国国家利益，有利于年轻一代学会冷静地分析历史问题，显示出编者是合格的历史学家。令人不解的是：为什么不向这些本国的同行学习呢？ <br />
<br />
二、关于战争过程。 <br />
<br />
　　1858年，大沽被占，英法侵略者兵临天津城下，英法俄美等国先后迫使清政府签订了《天津条约》。虽然丧失了不少权利，问题总算有个着落，双方还议定翌年在北京互换批准书，彻底完成法定程序。如果照双方的协议办理，导致火烧圆明园的英法联军再一次入侵是有可能避免。 <br />
　　可是，谁也没有料到纯属程序性的最后一步还会节外生枝，招来更大灾祸！教科书是这样写的：“1859年，英国公使和法国公使各率一支舰队北上大沽口，准备进京换约。清政府指定换约代表由北塘登陆，经天津至北京，并要求各兵船武装人员不得登岸。英法公使却仗恃武力，坚持要从大沽口溯白河进京。他们蛮横地率舰队闯入大沽口。防守大沽炮台的士兵开炮打击入侵者。炮弹准确地落在侵略军的军舰上，打沉了四艘，打坏了六艘，其余三艘挂起白旗逃跑了。在炮战的同时，侵略军900人企图登陆，也被打退。侵略军死伤几百人。大沽一带人民冒着枪林弹雨，给战士送饼送面，表现了高度的爱国热情。”在编者笔下，这是一曲爱国英雄进行曲，主角是士兵和普通百姓。可是，稍加推敲，便有很多疑问。 <br />
　　从后果看，这一仗显然打错了。翌年，英法联军再次入侵，招致北京被占，圆明园被烧。续订《北京条约》，不但规定原订的《天津条约》继续有效，还招来其他新损失：对英法的赔款分别由四百万两和二百万两一律增至各八百万两；割让九龙司；允许法籍传教士在中国自由传教，“并任法国传教士在各省租买田地，建造自便”，为日后连绵不断的教案种下祸根。如果不打，不是对中国更有利吗？ <br />
　　人们理所当然应该追问：公使走哪条路进京，真有那么重要，乃至不惜一战？双方意见分歧有没有认真交涉？真的是士兵自行开炮还是奉命行事？如果是前者，这是触犯军纪造成严重后果的大错，能算是爱国英雄的义举吗？如属后者，是奉了什么样的命令？ <br />
　　复查史实，这根本不是什么爱国英雄的壮举，而是愚昧的咸丰皇帝和僧格林沁亲王犯下的大罪。而且分歧不是教科书说的走哪条路进京，而是要英法使者绕个大弯进天津。当时在僧王幕下的郭嵩焘在日记中留下这样的记录：1859年4月10日“怡亲王至营……言奉旨密商一语：如夷人入口不依规矩，可悄悄击之，只说是乡勇，不是官兵。予曰：凡事须是名正言顺，须缓缓商之。怡邸愦愦可笑。僧邸商酌再三，欲令其由北塘入口，绕道至天津……辩论再三始定局，附片奏明。” <br />
　　《北京条约》订立后，他更具体叙述了当时的情况：“夷祸成于僧邸之诱击。去岁之役，先后奉诏旨十余，饬令迎出拦江沙外晓谕。洎夷船入内河九日，僧邸不一遣使往谕。去衣冠自称乡勇，薄而击之。仆陈谏再四，又虑语言不能通晓，两上书力争。”曾国藩对他的幕僚说过：“咸丰九年，洋人来换和约，僧忠亲王诱而击沉其船，天下称快。十年，夷人复至……京师不守，几丧天下。某谓僧邸此败，义当杀身以谢天下矣。”他们说的情况，同当时在现场的英国公使卜鲁斯的报告如出一辙。英法军舰6月16日已经到达，直到25日早晨才接到直隶总督恒福的照会，而当时军事行动已经开始。这些史料可以归纳为这么几点： <br />
　　1.咸丰皇帝决定在一定条件下，可以让官兵假扮乡勇，“悄悄”袭击洋鬼子。同时，他又十余次下令，要先“晓谕”洋人，先礼后兵。 <br />
　　2.僧格林沁忠实执行了“悄悄击之”的旨意，但没有事先晓谕；也坚决拒绝手下大臣的劝阻；并且是要洋人从北塘登陆，绕道至天津的设计者。 <br />
　　3.对这一丧权辱国的横祸，以曾国藩、郭嵩焘、吴汝纶等为代表(还包括李鸿章、冯桂芬等人)的比较清醒的官僚和士绅，已经有过严厉的批评和讽喻。 <br />
　　令人震惊的是：时至20世纪90年代，我们的教科书，仍然按咸丰皇帝和僧格林沁的调子唱歌，不同之处仅在把“乡勇”换成“士兵”！ <br />
　　说到这里，我们可以回答火烧圆明园是不是可以避免的问题了。面对咄咄逼人的强敌，作为弱势的大清帝国一方，明智的选择是严格执行现有条约，避免与之正面冲突，争取时间，改革和发展自己。而当时的政府和士绅，完全被极端的情绪支配，在小事上制造违约的蠢行，结果酿成大祸。如果清政府决策层和有关的地方督抚不是那么愚昧，这场灾祸是有可能避免的。可是，朝野上下的认识水平和专制的决策程序，是历史的积淀，不是朝夕所能改变；侵略的本性又决定了他们不可能成为文明之师；于是，这场灾祸又是难以避免的。<br />
<br />
是爱国壮举还是有悖文明行为 <br />
<br />
　　再来看看教科书的作者对义和团事件的评述吧。 <br />
