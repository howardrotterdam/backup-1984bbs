<br />
　　义和团兴起之初，袁世凯就上奏“其用以惑人者，谓能避枪炮。然迭与乡团、教民、兵役格斗，一遇枪炮，辄伤毙多人，瓦解鼠窜……其藉以动人者，谓图灭洋教。然上年春夏间，在曹州、济宁各属，掠教民一千一百余家，并掠及平民二百余家。秋冬间在东阳、济南各属，掠教民六百余家，亦掠及平民百余家。内多掳架勒赎之案，直与盗匪无异。故教民既被其殃，而平民亦多受其害。”而且有些地位比袁世凯更高的大臣(如北洋大臣、直隶总督裕禄等等)也提出了类似的意见。慈禧充耳不闻，比较清醒的众多大臣只好保持缄默，品质不良之辈则乘风转舵，谄上邀宠(如裕禄)。 <br />
