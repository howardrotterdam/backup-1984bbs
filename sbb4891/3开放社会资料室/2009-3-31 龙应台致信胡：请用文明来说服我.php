　　一、教科书没有只字提及义和团敌视现代文明和盲目排斥外国人以及外来文化的极端愚昧的行为。 <br />
　　义和团毁电线、毁学校、拆铁路、烧洋货、杀洋人和与外国人及外国文化有点关系的中国人……凡沾点洋气的物和人，必彻底消灭而后快。即使义和团真的立下了“扶清灭洋”的伟大功勋，也不能回避它的这些反文明、反人类的错误，何况正是这些罪恶行径给国家和人民带来莫大的灾难！这些都是众所周知的史实，也是中国人不能忘记的国耻，而我们的少年儿童必读的教科书却偏偏闭口不谈。 <br />
　　教科书也谈到拆毁铁路。它是怎么说的呢？“1900年6月……八国侵略军2000多人，由英国海军司令西摩尔率领，从大沽经天津向北京进犯。义和团拆毁从天津到北京的铁道，奋起狙击侵略军。侵略军在廊坊一带被义和团包围，死伤多人，狼狈逃回天津。”如此说来，拆毁铁路不过是抵抗侵略者迫不得已的措施。实际情况怎样？ <br />
　　1900年5月28日(阴历五月初一)，直隶总督裕禄致电总理各国事务衙门：“二十九夜(5月27日)先闻涿州至琉璃河一带猝被拳匪将铁路焚毁，讵今早由琉璃河至长辛店一百余里沿途铁道车站桥梁并局所洋房，均有拳匪蜂起焚烧”。与此同时，各地纷纷告急：“刻下电线又阻……至长辛店线阻，由琉璃河至涿州线，被匪徒砍断，所有电均被阻滞。”他们破坏这些设施完全出于对外来事物的敌视，而不是为了抵抗侵略者不得不采取的应急行动。同时，这类行动波及各地，不是局部性的偶发现象。也就是说，这是蓄意破坏财产的罪行，而不是某些史家说的抵抗侵略者的功勋。从时间看，西摩尔军从出发到被迫撤回天津是6月10日至26日之间的事，而在此之前拆毁铁路、电线，焚烧车站、抢掠财产的急报，已纷至沓来。义和团烧杀抢掠、敌视和肆意摧毁现代文明在前，八国联军进军在后，这个次序是历史事实，无法也不应修改。 <br />
　　二、教科书也没有谴责清政府高级官员及义和团乱杀无辜，烧杀抢掠的野蛮、残忍的罪行。 <br />
　　最有代表性的是山西巡抚毓贤的作为。六月初一(6月27日)，他将太原洋人办的医院烧掉，同时“将省中洋人，诱令迁居一处。当于教堂内搜出妇女二百一十一口，年老者数人，而五六岁十余岁至二三十岁者居多……于六月十三日，不动声色，带领兵勇，前赴洋人聚居之处，亲自兜拿。该洋人等尤敢拼力抗拒，奴才麾令勇敢数人，冒死突进，将洋人大小男女四十四口，及同恶相济的教民十七名，一齐擒获，立即绑赴市曹，同时正法”；“寿阳县秦锡圭获滋事之洋人七名口，押解前来，一并将其立正典刑。是晚北门教堂亦为拳民焚烧，省城洋人教堂已无遗迹”。当时的报刊还报道：“寓晋西人，得京师乱耗。群求毓贤保护。不料竟诱聚而歼之，且手刃数人焉。” <br />
　　毓贤的行为不是孤立的。所有不思进取的顽固分子，都是传统文化中最落后、野蛮的成分的继承者，这类顽固官僚趁机为非作歹的事例不胜枚举。例如，辅国公载澜是奉旨会同载勋、刚毅“统率”京津义和团的宗室，其残暴就不让毓贤：“京师乱起，载澜从拳匪入人家，大索，得毡布及他物，皆以教民论，扑杀之，虽宗室大臣不免”。 <br />
　　再看看义和团的所作所为吧。前人早已指出，不能把所有参加义和团的民众都视为匪徒，他们不少是盲从的愚民，但混迹其间的确实不少是土匪和流氓。总计在义和团事件中，全国各地1900年6月24日～7月24日期间，被杀外国人231名，其中儿童53名。他们大都死于义和团之手。至于中国的教徒(教民)和所谓“二毛子”被杀的，更是没有算清的糊涂账，其中绝大部分是被义和团杀死的，官兵也杀了一些。仅山西一省，就有中国天主教徒5700余人被杀。奉天(辽宁)全省“教民人命千余”。“而直隶(河北)全省杀人焚屋之案，几于无县无之。其杀人多者，一县竟至一二千名口”。甚至浙江亦“抢劫、焚毁教民家室至一千余家之众”。 <br />
　　“受害最烈”的北京，当时有关人士留下不少实录：1900年6月18日“城中日焚劫，火光连日夜……夙所不快者，即指为教民，全家皆尽，死者十数万人。其杀人则刀矛并下，肌体分裂，婴儿生未匝月者，亦杀之残酷无复人理”。“法国天主堂在西安门内西什库，刚相(刚毅)尝督兵攻之，亦不能破，拳实不敢前，哗噪而已。拳匪既不得志，无以塞后意，乃噪而出永定门。乡民适趋市集，七十余人悉絷以来；伪饰优伶冠服儿童戏物，指为白莲教；下刑部一夕，未讯供，骈斩西市。有妇人宁家，亦陷其中，杂诛之，儿犹在抱也……毓鼎上疏力争之，谓：‘谋乱当有据，羸翁弱妇，非谋乱之人；优装玩具，非谋乱之物……’疏入，狱已具”。 <br />
　　6月16日，“是日九点中，团匪烧大栅栏德记药房，延烧粮食店、灯市街、观音寺、珠宝市……共计店铺四千余家，火至天明未息。匪禁水会救火”。这个京师最繁华的地区于是毁于一旦。总的说来，“京师盛时，居人殆四百万。自拳匪暴军之乱，劫盗乘之，卤掠一空，无得免者。坊市萧条，狐狸昼出，向之摩肩击毂者，如行墟墓间矣。”这是所谓义和团“革命”的后果之一。 <br />
　　开头，民众与传教士和教民的矛盾令人同情；可是，他们后来的作为远远超过与外来宗教矛盾的界线。事件过后直至民国初年，朝野各界将这个组织定性为拳匪是有足够根据的。 <br />
