													<h2>龙应台致信胡：请用文明来说服我</h2>
						
						
																											<div id="postmessage_65703" class="t_msgfont">锦涛先生：<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;国民党主席马英九先生在二零零六年一月中勉励他的国青团青年学员时，说了这么一句玩笑的话：“希望将来国青团也能培养出一个胡.锦涛。”<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;我相信这是他从政以来所说过的最不及格的笑话。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;马英九先生很可能只单纯想到，“胡.锦涛”是从共青团体制里脱颖而出的国家领导人，但是会说出这样的话，也透露了他显然不曾更深刻地细思过，共青团是个什么样的体制？这个领导人所领导的“国家”，是个以什么为本的国家？他的权力来源是什么？正当性何在？在二十一世纪初掌握中国政权的“胡.锦涛”这三个字，代表了什么意义？<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;它当然代表了超高的经济成长指数，让世界惊诧，让国人自豪，可是同时，在政治自由的指针评比上，中国在世界上排名第一百七十七名。您可以说，这是以“西方右派”的标准来衡量的，不符合“中国国情”。好，让我们用一个社会主义的指针吧。追求资源分配的平等，不管均富或均贫，都是左派的核心理想吧？在贫富差异上，中国的基尼系数超过0.4，迫近0.45，这已是社会大动乱的门槛指针。指针数字下，多少人物欲横流，多少人辗转沟壑。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;也就是说，“胡.锦涛”三个字在二十一世纪的当下历史里，仍代表一种逆流：在追求民主的大浪潮中，它专制集权；在追求平等的大趋势里，它严重的贫富不均。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;在您刚刚上任时，人们曾经对年华正茂的您寄以期望，以为，作为一个新世纪的人物，您的心灵和视野会比您的前辈们更深沈，更开阔。共产党权力革命的杀伐蛮横之气，终究要被人文的体贴细致和文化的润物无声所取代。但是，两年了，我们所看见的，是什么呢？<br />
<br />
被割断的喉咙<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;促使我动笔写这封信的，是今天发生的一件具体事件：共青团所属的北京中国青年报“冰.点”周刊今天黄昏时被勒令停刊。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;在此之前，原来最敢于直言、最表达民间疾苦的南方周末被换下了主编而变成一份吞吞吐吐的报纸，原来勇于揭弊的南方都市报的总编辑被撤走论罪，清新而意图焕发的新.京.报突然被整肃，一个又一个有胆识、有作为的媒体被消音处理。这些，全在您任内发生。出身共青团的您，一定清楚“冰.点”现在的位置：它是万马齐里唯一一匹还有微弱“嘶声”的活马。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;而在一月二十四日的今天，这仅有的喉咙，都被割断。在“冰.点”编辑们正式得知这个“割喉”处分之前，所有跟“冰.点”有关的字和词，已经从网络上彻底消灭。<br />
<br />
在您的领导之下，网络警察的绝对效率，令人骇异。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;选在今天执“刑”，谁都知道原因：春节前夕，人们都已离开工作岗位，准备回乡围炉。报纸开始扑天盖地报导娱乐，制造温馨；电视开始排山倒海地表演联欢，生产快乐。选在这一天割断中国仅有的喉咙，然后让普天同庆的欢声把它淌血的声音遮住。行刑者蹑手蹑脚走开，过完年，一切都已了无痕迹。网络警察的效率和现代传媒的操弄，是您所呈现的二十一世纪统治技巧。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;网络警察动作快，是怕自己的人民知道；精算时间动手，是怕国际媒体知道。偷偷摸摸地执行，费尽心机地隐藏，泄漏的是政府的虚心和害怕。但是，请您告诉我这个困惑的台湾人民：这“和平崛起”大有为的政府，究竟为什么如此的虚心和害怕？<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; “冰.点”的停刊，其实没有人真正的惊讶，人们早在暗暗等待，好象一个宿命论者永远在等着鬼的半夜敲门索命；我发现，太多的灾难和压迫，使得大陆很少人相信好事会长久、梦想能成真、正义能落实。刊出龙应台的《你可能不知道的台湾》时，网络上已经四处流传“冰.点”被封杀的臆测；今天，只是“鬼”终于被等到了。而“冰.点”“勇敢”到什么程度使得共产党用这样阴暗的手段来对付它？<br />
<br />
仇外的建国美学<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;今天封杀“冰.点”的理由，是广州大学袁伟时先生谈历史和教科书的文章。因为它“和主流意识形态相对…攻击社会主义，攻击党的领导”。而“毁”掉了一份报纸的袁伟时先生的文章，究竟说了什么的话，招来这样的惩罚？<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;我认真读了这篇文章。袁伟时以具体的史实证据来说明目前的中学历史教科书谬误百出不说，还有严重的非理性意识形态的宣扬。譬如义和团，教科书把义和团描写成民族英雄，美化他对洋人的攻击，对于义和团的残酷、愚昧、反理性、反现代文明以及他给国家带来的伤害和耻辱，却只字不提。综合起来，教科书所教导下一代的，是“一、现有的中华文化至高无上。二、外来文化的邪恶，侵蚀了现有文化的纯洁。三、应该或可以用政权或暴民专制的暴力去清除思想文化领域的邪恶。”。对于这种历史观的教育，袁伟时非常忧虑：“用这样的理路潜移默化我们的孩子，不管主观意图如何，都是不可宽宥的戕害。”<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;锦涛先生，我不是不知道，共产党是以美化秦始皇、盗跖、太平天国、义和团这样一个历史脉络来奠定自己的权力美学的。我也不是不知道，每一个政权都会设法去建构一个所谓建国神话和图腾──您因此一定也很理解民进党的企图。但是，建构的国族神话里如果藏有仇外情绪，就是一个必须正视的危险。在二十一世纪，国界几乎快要不存在，地球愈来愈是一个紧密的村子，因为唇齿相依，不得不忧戚与共。中国为什么极力争取主办奥运和世博？目的不就是企图以最大的动作向世界推销一个新的中国形象：你看，中国是一个充满发展能量、爱好世界和平、承担国际责任的泱泱大国！<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;如果对外面的世界推销的是这样一个形象，关起门来教下一代的，却是“中华文化至高论”、“外来文化邪恶论”以及义和团哲学，请告诉我，哪一个中国是真实的？总书记能够光明磊落大声地告诉国际社会吗？<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;袁伟时说，教科书不能罔顾史实，不能赞美暴力，不能教下一代中国人对自己狂热，对外人仇视。这样的认知，锦涛先生，在我们这里，叫做“常识”。在北京，竟然是违反“主流意识形态”的入罪之论。那么能不能请您告诉我这个台湾人民，您的主流意识形态是什么？<br />
<br />
哪一个是你真实的面孔？<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;我们暂且不管大陆的知识分子和一般人民读者怎么看这“冰.点”事件，但是我很愿意和您分享像我这样一个台湾的知识分子的感受。至于龙应台这样思维的人在台湾有没有代表性，有没有影响力，您自己判断。<br />
<br />
我对中国大陆有着深切厚重的情感，来自命运血缘，历史传统，更来自语言文化。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;在台湾生长，我同时发展出与这一条“家国认同”情感线平行并重的执着，那就是对生命的尊重，对人道的坚持，而从这种尊重和坚持衍生出其它的基本价值：譬如主张独立的人格、自由的精神，譬如对贫富不均的不能接受，对国家暴力的绝不容忍，对统治者的绝不信任，譬如对知识的敬重，对庶民的体恤，对异议的宽容，对谎言的鄙视……<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;这一条我称之为“价值认同”的理性线。当“家国认同”的情感线和“价值认同”的理性线相互冲突时，我如何取舍？毫无犹豫，我选择后者。二十年前，我曾经写《野火》和国民党那个“家国”对抗；李登辉当政时，我曾经为文批判他的虚伪与狭隘；陈水扁不公不义，又迫使我执笔彻底抵抗。所以您如果闹不清我究竟是“统派”或是“独派”，不妨这样试试：台湾和大陆，哪边符合我的“价值认同”，就是我的“家国”。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;哪边违背我的“价值认同”，就是我离之弃之抵抗之的对象。如果两边都符合我的“价值认同”，那就开始讨论统一吧。所以，我是统派还是独派呢？<br />
<br />
以这样的价值结构来看今天“冰.点”事件，您说我这个台湾人看见什么？<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;我看见这个我怀有深切厚重情感的“血缘家国”，是一个践踏我所有“价值认同”的国度：它，把真理当谎言，把谎言当真理，而且把这样的颠倒制度化。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;它，把独立的知识分子当奴才使用，把奴性的知识分子当家仆使用，把奴才当──啊，它把鞭子、戒尺和钥匙，交到奴才的手里。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;它面对西方是一个脸孔，面对日本是另一个脸孔，面对台湾是一个脸孔，面对自己，又是一个脸孔。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;它面对别人的历史持一个标准，它面对自己的历史时──错了，它根本不面对。它选择背对自己的历史。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;它拥抱神话，创造假象，恐惧真相。他最怕的，显然是它自己。<br />
<br />
……<br />
<br />
您，还要我继续说下去吗？<br />
<br />
请说服我<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;我真正想说的是，锦涛先生，作为一个台湾人，我实在不在乎团团和圆圆来不来台北，虽然熊猫可爱得令人融化。但是我这样的台湾人可真在乎「冰.点」的安危，就像很多、很多香港人真在乎程.翔那个被逮捕的记者的安危。如果中国的「价值认同」是由一群手持鞭子、戒尺和钥匙的奴才在垄断它的解释和执行，而独立的人格、自由的精神是被打击、戒律、监控的对象，请问，我们谈统一的起点理由究竟是什么呢？而我对中国的情感还是有条件的，台湾还有很多热爱、深爱、无条件地执着地爱中国那片深厚土地的人──您又用什么东西去跟他谈统一，而他不致被人嘲笑、咒骂呢？<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;重点不在团团和圆圆，您知道吗？重点也从来就不在民进党，您明白吗？<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;重点就在「冰.点」这样具体而微的事情上，因为，说穿了，锦涛先生，您容不容许媒体独立，您尊不尊重知识分子，您用什么态度面对自己的历史，以什么手段去对待人民，每一个最细小的决定，都系在「文明」这两个字上头。经历过野蛮，我们不得不在乎文明。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;请用文明来说服我。我愿意诚恳倾听。<br />
