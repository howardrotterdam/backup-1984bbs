　　例如，对义和团事件的论述，它增加了这么两句话：“6月中旬以后，义和团群众开始围攻侵略者据点西什库教堂和外国使馆区。清政府却暗中派人给被围困的侵略者送去粮食、蔬菜、酒、水果等，表示慰问。”每句话都错得一塌糊涂！ <br />
