　　为了决定和战大计，从1900年6月16日开始，慈禧一连四天召集王公大臣六部九卿开御前会议。在会上吏部侍郎许景澄、兵部尚书徐用仪、户部尚书立山、内阁学士联元等人先后提出不能听信邪术、不可围攻使馆、不能主动对外宣战。总理各国事务衙门大臣袁昶和许景澄在两人联名的奏折中写道：“伏以春秋之义，两国k兵，不戮行人，泰西公法，尤以公使为国之重臣，蔑视其公使，即蔑视其国。兹若任令该匪攻毁使馆，尽杀使臣，各国引为大耻，联合一气，致死报复……以一国而敌各国，臣愚以谓不独胜负攸关，实存亡攸关也。”慈禧不但不接纳这些浅显的常识，而且大发专制淫威，把他们的脑袋砍掉！ <br />
