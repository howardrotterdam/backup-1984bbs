													<h2>雪狮与龙</h2>
						
						
																											<div id="postmessage_490760" class="t_msgfont">Melvyn C. Goldstein著&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; David Peng 译<br />
<br />
　　前言<br />
<br />
　　西藏问题，即与中国相关的西藏政治地位的长期冲突，是一个民族主义的冲突DD这是个富含感情色彩的争论，政治单元是否应直接与民族单元相吻合？西藏问题是一个矛盾的两面：一面是民族（西藏）自决和独立的权利；另一面是一个多民族国家（中华人民共和国）维护其认为的历史领土完整的权利。<br />
<br />
　　类似的民族冲突没有简单的答案。国际社会在此问题上也没有达成一致：一个民族什么时候可以正当地要求自决，或一个多民族国家什么时候有权阻止分离。目前的联合国宪章对此含糊不清。第一条第二款说，联合国的目的是保证“以尊重人民平等权利及自决原则为根据之友好关系”；但第二条第七款说，“本宪章不得认为授权联合国干涉在本质上属于任何国家国内管辖之事件。”[1] &lt;&gt;武力是最后的仲裁，例如美国用战争解决邦联政府脱离的威胁。[2] &lt;&gt;<br />
<br />
　　尽管西藏地处世界边陲，西藏问题引起了许多美国人和西方人的联想和同情，并在美国政坛得到共鸣。它也已成为中美关系中一个分歧点。但是对该冲突并没有很好的理解。在这个典型的民族冲突中，对领土控制的争斗，伴随着争相控制对历史和当代事件的表述。双方（包括他们的外国支持者们）定期用带着高度感情色彩，通常也是没有诚意的词汇来描述历史事件，影响国际认知并为他们的事业赢得同情。历史是主战场，双方用政治辞藻不透明地修饰冲突的事实，以至事实含糊不清。有兴趣的观察者们被相互矛盾的控诉和反诉淹没，即使是专家也极难不偏不倚、客观地评价该冲突。<br />
<br />
　　本书的目的是为了剥去这层修饰。接下来，本书将在现实政治（realpolitik）框架内，用平衡的方式对西藏问题进行剖析，重点放在参与各方的策略。<br />
<br />
　　尽管本书将讨论文化生存和人口转移等话题，本书并不特别讨论西藏的个人人权侵害问题，例如虐待犯人或逮捕和平政治示威的僧侣。这些人权问题存在，而且应受到严厉谴责，但他们不是问题的中心。西藏问题的存在远先于中华人民共和国的存在，也先于当代西方对普世人权的兴趣。事实上，如果西藏没有人权侵害，如果西藏人能够和平地发表不同政见，西藏问题仍然象现在一样充满争议。西藏问题是一个领土控制问题DD关系到谁统治它，谁住在那儿，谁决定西藏事务。<br />
<br />
　　我们必须先澄清“西藏”的含义。西藏人种分布的区域和西欧一样大。他们不止分布在中国，也在印度（拉达克[Ladakh]，锡金[Sikkim]，奈尼塔尔邦[Uttar Pradesh]北部和阿鲁纳恰尔邦[Arunachal Pradesh]），尼泊尔和不丹。在中国，1990年人口普查报告四百六十万西藏人分布在两个主要区域DD46%在西藏自治区(TAR)，54%在中国西部的青海，甘肃，四川和云南。[3] &lt;&gt;前者DD通常指“政治西藏”DD等同于近代达赖喇嘛政府统治区域。后者DD民族的西藏DD对应历史上不同西藏土着国家占领的边陲区域。休・理查德森(Hugh Richardson)，二十世纪三十年代和四十年代在拉萨(Lhasa)担任殖民地印度政府官员的英国外交官，这样解释这种区别：<br />
<br />
　　直到1951年，西藏政府连续统治着“政治”西藏。这片区域以外，往北和东[（民族DD译注）西藏的安多(Amdo)和康(Kham)]……是它的“民族”扩展，藏人一度占据那儿，现在也仍然占多数。在这片更大的区域，“政治”西藏仅在一些地方间断行使管辖权；在大多数地区，当地世俗或僧侣长官控制这些大小不断变更的区域。18世纪，汉人开始零星渗透这些区域。[4] &lt;&gt;<br />
<br />
　　历史上民族西藏和政治西藏的这种区别成为西藏问题的各自表述的战场的一部分。例如，因为西藏流亡政府的一个主要政治目标就是重新统一中国的所有藏区为一个“大西藏”，他们通常使用“西藏”这个词来表述民族西藏区域和政治西藏区域发生的事件，试图给人这样一种印象，近代史上存在着“大西藏”。如此，尽管政治西藏1950年10月被入侵，西藏流亡政府宣布西藏早在1949年即被入侵，当时，中国军队“解放”了民族西藏的青海，四川和甘肃省藏区。[5] &lt;&gt;类似的，为了制造一种印象，即20世纪三十年代和四十年代西藏已是中国的一部分，中国政府宣称，西藏代表参加了中国政府的会议，暗示他们来自拉萨，但实际上他们来自民族西藏。为了避免这种混淆，除非另外注明，本书中“西藏”一词指的是政治西藏。<br />
<br />
　　很难以现代西藏这样一个有争议的主题着书，因为很多关键信息来自要求匿名的个人。尽管如此，让我广泛地描述本书中所用到的原始资料。<br />
<br />
　　一个重要的信息来源是中国媒体，例如，美国国外广播信息服务(FBIS)译本包含的内部广播。另一个消息来源包括西藏流亡政府（或他的支持者们）发布的材料，例如，国际声援西藏运动的《西藏新闻观察(Tibet Press Watch)》，或者《世界西藏新闻(World Tibet News)》。伦敦的西藏信息服务(Tibet Information Service)发表的报告和档案提供了另外一个有用的数据和分析的信息来源。<br />
<br />
　　除此之外，我自己在中国的大量的实地调查提供了重要的数据库。过去12年来，我在西藏进行了广泛课题的研究，包括语言、游牧民、僧侣、现代历史和农业发展。我在那居住了整整两年。这些研究使得我能得到城市和农村生活的第一手观察；而且，因为我能说、读藏文，我能在无需向导和翻译的情况下容易地和各行各业的藏人打成一片。很多藏人乐于和我分享他们的看法和观点，因此，尽管他们的名字没有出现在本书中，我愿表达对他们的感谢。同样，我感谢中国，西方和西藏流亡社区的许多官员，学者和知识分子，他们也和我讨论一些重要的问题和事件。不幸的是，他们也必须保持无名。尽管有这种帮助，我为本书中的观点负最后责任。<br />
<br />
　　另一方面，如果我不感谢我的研究的赞助方，那会是一种失职。他们是，美国与中国学术交流委员会(the United States's Committee on Scholarly Communication With China)，国家地理协会研究和探险委员会(the National Geographic Society's Committee on Research and Exploration)，美国国家人文研究捐赠基金(the U.S. National Endowment for the Humanities)和美国国家科学基金会(the U.S. National Science Foundation)。我也非常感谢凯斯西储大学(Case Western Reserve University)，他们慷慨资助我在西藏的研究工作，促进我和拉萨的西藏社会科学院的长期关系。<br />
<br />
　　最后，我想感谢本书的编辑，加州大学出版社(University of California Press)的Sheila Levine和Laura Driussi。他们对这个项目的支持和他们加速本书出版的能力堪称奇迹。<br />
