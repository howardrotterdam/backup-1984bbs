																											<div id="postmessage_413708" class="t_msgfont">太祖武帝开国元年 49 <br />
二月，王师入京，吏民欣喜。北土悉平。帝遣六军征南朝，号百万。 <br />
四月，六军涉江，灭南朝。南酋蒋氏遁，入海，据澎湖以抗圣化。迁九鼎于京。 江南诸道初定。 <br />
十月，帝登基于天安门。入太庙，誓，立政，改元“开国“。周文正公相。封六辅政亲王。 <br />
帝曰：“华夏诸民今日立也！“ <br />
十一月，骠骑将军刘，西路招抚使邓统王师十万入蜀，前朝余孽星散，蜀，滇，黔传徼而定。 <br />
邓平公封西南节度使，坐镇西南以图吐蕃。 <br />
是年，帝下《别了，司徒雷登》等三t，用赐西夷美利坚使，夷使惭去。史称“三t去西夷”。 <br />
太祖武帝开国二年 50 <br />
二月，北狄俄罗斯遣使来朝，盟，世世代代约为兄弟之邦。 <br />
六月，高丽内乱。帝t命停战，不听。 <br />
九月，高丽北朝胜。同月，西夷美利坚纠合西夷数十部落侵朝鲜，号“联合国军”。 <br />
十月，高丽北朝溃，美夷之师次于鸭绿水。帝以前将军彭桓公为东征元帅，加太子少保，太子岸英为监军，统精锐二十万入高丽，战于长津，美夷败绩，遗尸数万，山林溪涧边，虏血几洒遍。 <br />
征西将军王震统兵入西域，设西域都护府，立生产建设兵团。西域归王化。 <br />
太祖武帝开国三年 51 <br />
十一月，行“三反”。 <br />
是年，王师美夷战于朝鲜。美夷惧，求和，帝不许。吐蕃归王化。自是，天下几一统。太子战死沙场，天下悲恸。 <br />
太祖武帝开国四年 52 <br />
一月，行“五反”。 <br />
八月，帝下“削藩令”，令东北节度使高岗王（开国辅政六亲王之一），华东节度使饶石公，西南节度使邓平公，中南节度使邓恢公，西北节度使习勋公进京。时人谓之：“五马进京，一马当先。” <br />
是年，王师美夷再战于朝鲜。王师据上甘岭，敌纠兵数万以攻，终溃。 <br />
太祖武帝开国五年 53 <br />
一月，行“新三反”。 <br />
三月，俄狄王斯大林死。（注：狄王死不曰崩。） <br />
五月，与俄狄盟。 <br />
六月，始料民，口六亿又一百九十又三万八千又三十又五。为前所未有之盛世。 <br />
九月，彭桓公平美夷于高丽。高丽中分。北者为朝鲜，南者为韩国。 <br />
太祖武帝开国六年 54 <br />
帝使使日内瓦，南越中分。 <br />
五月，故东北节度使高岗王自杀于北京，天下震惊。谥“幽”，“动祭乱常曰幽”。 <br />
八月，江淮大水，平地汹涌不见寸土。 <br />
九月，行布票。天下币制革新由是而始。 <br />
十二月，御史劾故东北王高，华东王饶怀不臣之心阴谋乱上。帝令有司查办。 <br />
太祖武帝开国七年 55 <br />
五月，帝使周文正公盟诸王于万隆。 <br />
七月，行“肃反“。帝下《关于农业合作化问题》t。南越来朝，赐贝八亿朋。 <br />
十月，帝下《农业合作化的一场辩论和当前阶级斗争》t。 <br />
是年，帝大封开国诸将，计元帅十，大将十，上将若干。满朝文武，人才济济。 <br />
太祖武帝开国八年 56 <br />
五月，帝曰：“百花齐放，百家争鸣“。右党扰乱朝纲自是起。 <br />
七月，帝曰：“美帝国主义是个纸老虎。” <br />
是年，埃及来宾，匈奴来盟。八方万国，皆慕我中华圣化。 <br />
太祖武帝开国九年 太祖反右元年 57 <br />
五月，帝逐右党。改元：反右。 <br />
十月，帝曰：“大鸣、大放、大字报、大辩论”。 <br />
十一月，帝西巡俄狄，曰：“东风压倒西风。“ <br />
太祖武帝反右二年 58 <br />
二月，诰告天下，命灭“四害”。“四害”者，蚊、蝇、鼠、雀也。 <br />
六月，左丞相薄作《两年超过英国》上疏，帝大悦，嘉之。 <br />
七月，俄狄王赫鲁晓夫来朝。 <br />
九月，桂人来告田可亩产十三万余斤者。 <br />
十一月，滇民有饥色。 <br />
是年“大跃进“，万民炼钢，卫星纷飞。东征大军自高丽还。 <br />
太祖武帝反右三年 59 <br />
吐蕃叛，帝命征之。王师势如破竹，吐藩达赖逃往天竺。吐藩乃定。 一月，帝下《“反瞒产私分”》t，令天下百姓不得隐瞒粮食。 <br />
二月，诸侯朝帝于郑。 <br />
七月，帝大会诸侯于庐山。彭桓公上《万言书》，以军人干政事。帝怒。 <br />
八月，下彭桓公于狱，尽逐右党。野有“功高震主，鸟尽弓藏”之议。是年，民有饥色。 <br />
太祖武帝反右四年 60 <br />
民大饥，赤地千里，野有饿孚，豫桂尤甚。赈饥民。 <br />
四月，帝命周文正公巡信阳。帝不食肉。 <br />
六月，帝至沪上。 <br />
七月，俄狄背盟。 <br />
十一月，t令六军将士“两忆三查“。“两忆三查”者，“忆阶级苦、忆民族苦、查立场、查斗志、查工作”也。 <br />
太祖武帝反右五年 61 <br />
大饥。赈饥民。帝以“天道不常“传位于刘哀帝。哀帝始摄政，未改元。 <br />
是年，太祖再会诸侯于庐山。 <br />
哀帝反右六年 62 <br />
民饥。 <br />
四月，伊犁饥民乱。 <br />
六月，彭桓公谏以“八万言书”，帝怒。 <br />
七月，诸侯朝高祖于北戴河，高祖曰：“阶级斗争必须年年讲、月月讲、天天讲。” <br />
九月，天竺犯我吐藩。 <br />
十月，王师伐天竺，大败之。天朝声威远播。 <br />
哀帝反右七年 63 <br />
二月，在京。太祖曰：“阶级斗争，一抓就灵”。 <br />
三月，太祖大诰天下曰：“向雷锋同志学习”。 <br />
九月，锡兰来宾。 <br />
十二月，周文正公出巡亚非拉十四国。天下万国，竞相慕我中华上国之风采。 <br />
是年，我朝石油自给。 <br />
哀帝反右八年 64 <br />
五月，《太祖语录》始颁行。 <br />
六月，行“样板戏”。京津沪，民有奇装异服者。 <br />
十月，原子弹爆。 <br />
十二月，行“四清”。 <br />
哀帝反右九年 65 <br />
一月，太祖颁《二十三条》。 <br />
七月，前朝代宗李宗仁自海外归降。太祖携帝慰之。 <br />
十月，太祖曰：“不要怕造反。” <br />
九月，结晶牛胰岛素成。 <br />
十一月，大学士姚作《评新编历史剧〈海瑞罢官〉》，“文革”初始。 <br />
哀帝反右十年 太祖文革元年 66 <br />
改元：文革。 <br />
三月，邢台地震。 <br />
五月，“文革“始行。罢科举。 <br />
七月，太祖泅水于江，遂至于京师。 <br />
八月，百万红卫朝王于天安门。天下始大乱。 <br />
太祖武帝文革二年 67 <br />
一月，上皇废哀宗，哀宗请骸骨，帝不许。太祖重秉政，颁《公安六条》《六军支左》。 <br />
二月，“二月逆流”。 <br />
三月，黜左丞相薄。 <br />
五月，天下始行“忠字舞”、“早请示，晚汇报”、“语录歌”、“语录操”。 <br />
八月，英夷使馆火。 <br />
是年，颁《太祖语录》八千六百余万。 <br />
太祖武帝文革三年 68 <br />
黜哀宗刘、右丞相邓。 <br />
是年，吐蕃，西域革委会成立。 <br />
太祖武帝文革四年 69 <br />
林庄公赞曰：“大海航行靠舵手，干革命要靠毛泽东思想！” <br />
三月，王师败俄狄于珍宝洲。 <br />
四月，九大，封林庄公为亲王，加九锡，赐林亲王天子旌旗，出入免跪拜。 <br />
十一月，杀哀帝于开封。其卒不赴告。 <br />
太祖武帝文革五年 70 <br />
春，林庄亲王欲上帝尊号，帝不悦。 <br />
四月，卫星上天，红旗未落地。 <br />
八月，帝会诸侯于庐山。 <br />
十二月，帝会夷人斯诺。周文正公主持修建葛洲坝。 <br />
太祖武帝文革六年 71 <br />
一月，陈懿公薨，帝往悼之，曰：“陈毅是位好同志。”加封王，谥“懿”，法曰：“温柔贤善曰懿“。 <br />
八月，帝南巡。 <br />
九月，林庄亲王叛。欲弑帝，帝觉，攻林庄亲王，庄亲王北奔，死于漠北。谥“庄”，谥法曰：“死于原野曰庄“。臣而作乱，贬爵号为伯，厥死不言薨。 <br />
太祖武帝文革七年 72 <br />
美夷，倭奴来盟。 <br />
太祖武帝文革八年 73 <br />
一月，南越平。 <br />
三月，重起用邓平公为右丞相 <br />
七月，皇孙远新封张铁生“白卷英雄“。 <br />
十二月，帝令六军各提督对调。 <br />
太祖武帝文革九年 74 <br />
二月。赞比亚国王来朝。帝提出三个世界之划分。 <br />
十一月，大元帅，彭桓公薨。谥“桓“，谥法曰：“辟土服远曰桓。”。 <br />
是年，发秦始皇陵。 <br />
太祖武帝文革十年 75 <br />
是年，前朝思帝死于东海夷洲。谥法曰：“追悔前过曰思”。王而失国，厥死不言崩。 <br />
太祖武帝文革十一年 惠宗凡是元年 76 <br />
帝病，道扬末命于公子华，曰：“你办事，我放心。”，公子华嗣训。 <br />
一月，周文正公薨。谥法曰：“慈惠爱民曰文“，“刚直不阿曰正”。国人悲之，执手泣别。 <br />
四月，国人益悲，集于天安门。 <br />
七月，地大震，死伤无数，丧丁二十八万余口，帝惊。 <br />
是月，大将军，朱武公薨。 <br />
八月，帝崩。天崩地坼，万民震恸，如丧考妣。谥武。谥法曰：“克定祸乱曰武”。公子华立，是为惠帝。惠帝命“两个凡是”，改元：凡是。 <br />
十月，四凶作乱，叶定公执四凶。谥法曰：“刚强直理曰武”。 <br />
惠帝凡是二年 77 <br />
八月，“文革”罢，天下初定。重开科举取士。南斯拉夫王铁托来朝。 <br />
是年，邓平公摄政。惠帝畏之。 <br />
惠帝凡是三年 78 <br />
一月，南越背盟。 <br />
十一月，邓平公游美夷。 <br />
是年，拨乱反正。 <br />
惠帝凡是四年 79 <br />
二月，南越背我，邓平公命六师伐之。 <br />
四月，邓是年，始行“计划生育“。 <br />
惠帝凡是五年 宣帝改革元年 80 <br />
五月，葬刘哀帝。谥法曰：“恭仁短折曰哀”。邓平公悼曰：“伟大的无产阶级…永垂不 <br />
朽。” <br />
十一月，邓平公与诸大臣共废惠帝，邓平公立，是为宣帝。 <br />
是年，改革开放。 <br />
宣帝改革二年 81 <br />
五月，开国女亲王宋薨。维新立王，率巡大卞，用变和天下。 <br />
宣帝改革三年 德帝耀邦元年 82 <br />
七月，料民，口十亿。 <br />
是年，上退位称太上皇，监政，诏胡德公即帝位，改元耀邦。 <br />
德帝耀邦二年 83 <br />
是年，设六师行署于京师。 <br />
德帝耀邦三年 84 <br />
王师肆伐南越。 <br />
德帝耀邦四年 85 <br />
是年，裁甲士四分一。 <br />
德帝耀邦五年 86 <br />
后将军叶定公薨。谥法曰：“安民大虑曰定”。 <br />
十二月，众翰林大学士上书斥污吏。 <br />
德帝耀邦六年 仁帝**元年 87 <br />
一月，上皇废德帝，以为德亲王。立亲王赵，是为仁帝。改元：**。 <br />
仁帝**二年 88 <br />
是年，物价飞涨。王舟破南越于南海。 <br />
仁帝**三年 孝帝整顿元年 89 <br />
二月，美夷来宾。 <br />
三月，吐藩乱。 <br />
四月，德亲王耀邦薨，谥法曰：“绥柔士民曰德“。京师大乱，暴民起于萧墙，波及天下。 <br />
唯帝治沪上，安民伐乱，一时东南平定，万民悦服。 <br />
五月，两江总督江入京。 <br />
六月乙酉，国人暴动，李丞相奉上皇旨平京师乱，废仁帝。壬辰，江公泽民登基，是为孝帝，改元：整顿。 <br />
九月，上皇始退而不朝。 <br />
孝帝整顿四年 孝帝市场元年92 <br />
二月，太上舜巡，百越南海诸民迎之，吏民欢欣，太上喜，下南巡旨，乃改元：市场。 <br />
孝帝市场四年 95 <br />
革顺天府尹陈，九门提督王，王自缢以谢罪，陈交部议处。天下万民喜而奔走。 <br />
孝帝市场五年 96 <br />
帝观兵于台湾海峡。百万将士三呼万岁，足为一时之观。有西夷兵临东海窥之， <br />
帝镇定不为动，夷乃去。 <br />
孝帝市场六年 97 <br />
二月，太上崩，天下举哀七日。谥法曰：“圣善周闻曰宣”。 <br />
七月，逐英夷于九龙。 <br />
孝帝市场七年 98 <br />
三月，以容基为丞相。 <br />
是年夏，洪甚，湖广，两江受其害，嘉鱼平地水深一丈。广州，金陵，济南各府均出兵数万解万民于倒悬。军民鱼水，堪为千古佳话。各地大开官仓赈灾。上诏免灾地三年钱粮。是岁，仓廪实，民生无碍。 <br />
孝帝市场八年 99 <br />
元月，帝登京师世纪坛。 <br />
五月，美夷炸我驻南使馆。帝斥之，夷惶恐，献布帛钱粮若干。太学生群起于宫门。帝t曰：不为己甚。 <br />
十二月，逐葡夷于澳门。自是逐尽洋夷，唯前朝余孽尚据澎湖以抗圣化。 <br />
是年，再裁甲士50万。 <br />
孝帝市场九年 00 <br />
四月，有邪教***造乱于宫门。 <br />
七月，帝下诏除之。 <br />
是年，帝锐意吏治，诛广西巡抚，加大学士成克杰，江西布政使胡长清。厦门大贾赖昌星贿乱朝纲，闽浙官员，多有暗通者。值御史劾之，乃命有司稽查。赖惧，遁于西戎。党羽多被查办。吏治为之一振。 <br />
孝帝市场十年 孝帝德治元年 01 <br />
正月，帝再登兰吞，下诏，以德为治国之本，乃改元：德治。是月，乱民自焚于宫门。 <br />
二月，京师西五十里地震，大风沙。 <br />
三月，江西乡校爆炸，士民震怖。未及，大盗龚某作乱于石家庄，人心惴惴。 <br />
四月，西夷名美利坚者遣兵窥境，尽为我所擒。乃诏谕夷使，严词责备，夷酋布殊大惧，顿首泣血而为书，谢罪殿前。上宽仁，念彼夷兵家小亦有倚门之苦，皆予开释，令其团聚。八方万国，由是遍赞中华上邦之厚德。 <br />
五月，京师再地震。国子监亦不免。太学生惶惶。 <br />
