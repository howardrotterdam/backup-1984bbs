																											<div id="postmessage_782728" class="t_msgfont">2010.8.14 危情<br />
<br />
[HKIMDB]http://www.hkmdb.com/db/movies/view.mhtml?id=7662&amp;display_set=big5<br />
[豆瓣]无<br />
[时光]http://www.mtime.com/movie/22412/<br />
[百度百科]http://baike.baidu.com/view/611994.htm?fr=ala0_1_1<br />
<br />
为什么是《危情》第一个出场？其实我不能总结一个段落大意后给自己一个中心思想，来告诉自己为什么《危情》要第一个出场，反正就是第一了。<br />
<br />
资料显示这是1993年的电影，正是我如痴如醉流连于录像厅的年代，但我已经忘记了是否在录像厅里看过，反正这次看的时候宛如处女，全是新的。看得可乐，虽然缺乏了点回忆。<br />
<br />
我的香港电影观影经历始于上世纪的80年代初期，在镇上卫生院的院子里，夏日的晚上，湛蓝的夜空下非常的凉爽，一台彩电架在高的凳子上，围坐了几十人，看的是一个武打片。当然在那个卫生院里我还看到了《霍元甲》。前几年回去时，那个卫生院还在，虽然很破旧了，但还在原地，就是旁边多了很多难看单调的水泥门脸房子。希望以后回去时，它还在。<br />
<br />
第一次的记忆是那么的清晰，第一次KISS，第一次SEX。高雅的同学自然不会记住那些低俗的事，他们肯定会会记得第一次向组织上掏心窝，第一次交D费一类的事，当然这与我无关，我打小就是个低俗的人。所以第一次看录像我都记得那么清楚……自从D的十一届三中全会后，改革的春风吹遍神州大地，吹到我们这个偏僻的小镇上，我第一次看到了录像，看了香港片子。小时候所有的作文开头都是这样写的。<br />
<br />
这个片子记忆犹新，后来才知道是《独臂刀》，张彻的牛片。不幸的是，这不是三级片。<br />
<br />
所以第一次看三级片的经历一拖就拖到80年代末期了。录像厅已经开始遍布，每天在门口放一块小黑板，上面用红黄白几种粉笔写上香港枪战片××××，××××，××××，票价5毛。那时候，在桌球厅打一盘桌球也是5毛。由此看来，在我国5毛的历史最少有30年了，没想到呀，在北京，5环外都2万多一平的房价的今天，5毛还在，而且还比以前多很多。<br />
<br />
扯远了，说《危情》<br />
<br />
首先可以肯定的是陈雅伦老师脱衣服，露点了，用三级行话叫“三点全露”，毛毛也看得很清楚。陈老师年轻时的mimi不大，莫约B Cup，很挺。<br />
<br />
三级片也分，有些三级片没有女角们脱衣服也是一部不错的故事片，比如以后会说道的舒淇的《悍妇岗》。有些三级片要是没有女角――尤其是女主角脱下衣服，这片就没法看下去了。现在还好，有了鼠标，可以直接拖拽。搁20年前，只能是大家在闷不透风黑漆漆的录像厅里开始低声骂娘，然后有大胆的哥们高声叫骂一两嗓子，然后逐渐汇集成大家共同的心声“老板，换片！”，一起鼓掌，齐声喊。如果万幸是在电影院改成的录像厅（整个80年代末开始到90年代，大部分中小城镇的电影院都改成录像厅、桌球厅、家具城了）就直接敲打座位。鼓掌换片一事只发生在巷子里那些小录像厅，只能做板凳、条凳。<br />
<br />
非常不幸，本片就属于后者。要是陈老师不脱那下衣服，这片就直接删掉得了――还得按住Shift。当然，当年片商也是以陈老师全脱作为买点了，还取得极好的票房。<br />
<br />
本片剧情相当弱智。其实弱智也分的。比如舟舟，大家都知道他是，但他努力做自己，充分发挥自己的音乐上的天分，终于在指挥这个岗位上取得骄人的成绩。还有一种，特别认真的上大学，读博士，做研究，发表各种石破天惊的见解来诠释《孙子兵法》，听说最近还做了将军，可惜的是，大家仍旧认为他是。<br />
<br />
再次非常不幸，本片就是这样的，陈老师和王老师（男猪脚王敏德，灰常灰常帅灰常灰常阳光，当然那个年代帅哥不去拍三级片脑子同意手还不同意呢，所以王老师也演过很多三级片，从资料里看我看过大约有4-5部）灰常灰常努力认真的表演，一点没觉得编剧\导演（卢坚，卢老师）有多弱智。<br />
<br />
还好，万幸年轻时的陈老师是天使面容魔鬼身材，又是准一线明星，想在大屏幕上一睹陈老师三点尽露激情演出的低俗影迷肯定很多，不然票房也不可能有670多万港币。就比如搁现在，林老师志玲姐姐要是打算三点尽露的拍个三级版赤壁，肯定比阿凡达还火爆，我都会去凌晨1-2点去排队买票。票房也肯定不是《唐山大地震》这5亿能打得住的，最少20亿起。<br />
当然章老师子怡和张老师柏芝没戏了，大家已经都看过了。<br />
<br />
可惜我没给陈老师贡献过一分钱就看了人家美妙的胴体，面壁去～<br />
