<br />
一晃20多年过去了，香港电影的辉煌也过去了，留下来只有一些回忆和几十个G的片子（还在陆续增加中）。<br />
<br />
上个月脑子里某个地方错乱了一下，想看三级片，一念之下，一发不可收拾，下了有上百部香港三级片，干脆做了点整理工作。胡乱看了几部后，发现趣味横生，于是动了写点什么的念头。<br />
<br />
想写这个文章的时候，郭德纲还住在京郊大兴某处有违章建筑的别墅里，伟大光荣正确的D还未开始反三俗。虽然现今的社会谁都知道俗不可耐，无道德无廉耻，但D不反三俗，三俗就是不存在，P民们看到的听到的感受到的都是幻觉。<br />
<br />
吾生也晚，虽然有幸在短短30多年已经经历了不下20次的“百年一遇/千年一遇”，但却不幸错过了一系列轰轰烈烈的镇反三反五反反右大跃进人民公社四清整风整社破四旧大串联文攻武斗批林批孔批邓批四人帮。行文至此，汗流浃背，羞愧万分。不想几日后，D领导下CCTV指导下BTV执行下的伟大的反三俗运动终于在我有生之年轰轰烈烈的展开了，本文如果留而不发就显得很没有诚意，很不响应D中央反三俗的伟大号召。<br />
<br />
色字一闪念，决心忆甜睹苦，每日看一部三级片，写一段影评，供广大高雅之士在国家大剧院聆听完少将美妙的歌剧后批判之用。<br />
<br />
大部分的片子在豆瓣和时光网上都有介绍和影评了（豆瓣删掉不少特别知名片子的页面），每部片子我都会给出豆瓣和时光网、百度百科上的地址（插播广告：搜毛片、三级片、破解软件还得用百度呀！）一般不会涉及剧情介绍，重看的目的是怀旧和从新视角来看到新的细节，期望引起同好的共鸣和分享。<br />
<br />
当然文章最后会有一串乱码一样的东西。<br />
