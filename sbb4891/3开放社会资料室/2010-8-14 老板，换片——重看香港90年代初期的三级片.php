																											<div id="postmessage_783860" class="t_msgfont">老板，换片！之二《足本玉蒲团》<br />
――重看香港90年代初期的三级片<br />
2010.8.15<br />
<br />
[HKIMDB]http://www.hkmdb.com/db/movies/view.mhtml?id=6961&amp;display_set=big5<br />
[豆瓣]无<br />
[时光]http://www.mtime.com/movie/36418/&nbsp;&nbsp;能搜到，但打不开，估计被屏蔽了<br />
[百度百科]http://baike.baidu.com/view/1519008.htm?fr=ala0_1_1 百度百科上本条目的信息是HKIMDB中1986年版《玉蒲团》的信息<br />
<br />
看片几十年，从低俗如毛片A片装B片三级片到高雅如主旋律片，怎么着也有个几千部了，闭上眼睛要我马上说出5部印象最深刻的电影，我想《足本玉蒲团》肯定是第一个跳出来的，D中央要是知道了，一定会说你丫太低俗了！必须反掉。但这实在是没法子的事情，虽然我每念于此，诚恐诚惶，也努力想多看看第一书记一类的高雅作品来陶冶情操，但我看这类片子时总按奈不住想要操。<br />
<br />
颇有喜感的事情是，本片虽然名《足本玉蒲团》，但实际上我少年时看过的全是洁本的，而且剪得干干净净，比付笛声他老婆用过妇炎洁后还干净。其实大家都知道本片从没正式引进到大陆，也就是说剪刀手广电局在盗版商那也有5毛（毛少将估计都知道盗版商毫无剪掉裸露镜头的必要）。当然年少无知如你我对此全无了解，也不知道有广电局老头老太们的存在，反正片子香艳旖旎正要宽衣解带，几十双瞪圆怒目在灰暗污浊的空气里闪烁时，画面一换就成了男女猪脚在厅堂读书。NND！<br />
<br />
满腔怨火撒在录像厅老板进的片不好上，于是骑着单车踏遍了全市每一家录像厅。是的，每一家。成年后，我时常怀疑我是否真的为看足本的玉蒲团真的找遍了市里的每一家录像厅，还仅仅是为了在回忆里让自己显得更NB而编造的谎言来麻痹自己，或者仅仅是发下“看遍全市每个录像厅”的宏愿后并没做到后的自我安慰？经过反复确认，我可以肯定我的汗水流淌在全市每一家录像厅里头。今年年初回家，在老市区，我都还能记得每家录像厅的大门朝那边开，当然顺带想起来那些已经消失很久的租书摊和游戏机厅。<br />
<br />
总之，我看全足本玉蒲团一事居然是在香港已经拍不出好三级片我也不看三级片的21世纪了，这事颇让我觉得有些啼笑皆非。类似当年上学时酷爱踢球，做梦都想有套正规的球衣，有双钉鞋。但从没拥有过。等我买得起正版球衣和钉鞋时，我已经不踢球了，继而到现在连足球都不看了。<br />
<br />
无论是在记忆里，还是现在重看，《足本玉蒲团》都是所有玉蒲团系列里最好的那部。我后来时常把著名的《玉蒲团之偷情宝鉴》与这部《足本玉蒲团》混淆在一起，总觉得不是我想的那部，虽然也很好看。<br />
<br />
《足本玉蒲团》的女角选得非常正点，比后来的各种版本玉蒲团都更吻合古装色情片气质。身材高挑，容颜美貌，着衣时冷艳高贵，脱衣后放肆淫荡。三个女角分别是郑婉雯、周迎翘、于芷蔚，尤其是于芷蔚老师演的瑞珠让我20多年来念念不忘。下面会放一些于老师的截图为证。男角方面，倒是《玉蒲团之偷情宝鉴》里徐锦江老师演的权老实，吴启华老师演的未央生更吻合。<br />
<br />
《足本玉蒲团》的编剧是文隽老师，当然谁不会知道20多年后他会成为香港电影金像奖主席，舒淇老师就是他带入行的。可以想像文老师也是性情中人，和黄老师一样，很低俗不装B。<br />
<br />
有评论说何蕃老师的《足本玉蒲团》拍得太拘谨，初看是个色情片，看完才知道是个教育片。完全没有麦当雄老师的《玉蒲团之偷情宝鉴》那般放肆且奇淫乖巧花样百出，但对我这样打小就渴望高雅的有志青年而言，忠于原著才是王道，因为看过《足本玉蒲团》后我在我市长途汽车候车厅“弄”了一本《肉蒲团》，书，纸质的书。“妇人脐下三寸之物乃生我之门，亡我之户”看得我醍醐灌顶，至今时刻铭记在心。<br />
