　　用动物对人类抗争后自建家园来再现前苏联的整个历史情形，奥威尔的《动物庄园》被公认为二十世纪最杰出的政治寓言。《动物庄园》被译成二十多种文字在全世界流传，其中的有些语言还变成了人们的口头禅，和《1984》并称为乔治&#12539;奥威尔最重要的代表作。此版本收有小说的英文原文。<br />
<br />
　　本书以隐喻的形式写革命的发生以及革命的背叛，自然还有革命的残酷：一个农庄的动物不堪主人的压迫，在猪的带领下起来反抗，赶走了农庄主；它们建立起一个自己管理自已的家园，奉行“所有动物一律平等”的原则；两只领头的猪为了权力而互相倾轧，胜利者一方宣布另一方是叛徒、内奸；猪们逐渐侵占了其他动物的劳动成果，成为新的特权阶级；动物们稍有不满，便招致血腥的清洗；统治者需要迫使猪与人结成同盟，建立起独裁专制；农庄的理想被修正为“有的动物较之其他动物更为平等”，动物们又回复到从前的悲惨状况。明眼的读者自可看出，此书不属于人们所熟悉的那种蕴含教训的传统寓言，而是对现代政治神话的一种寓言式解构。<br />
