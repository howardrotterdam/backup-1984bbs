<strong>作者简介：</strong><br />
<br />
　　乔治・奥威尔（George Orwell，1903年6月25日－1950年1月21日），原名艾里克・阿瑟・布莱尔（Eric Arthur Blair），英国左翼作家，新闻记者和社会评论家。<br />
<br />
　　《动物庄园》和《一九八四》是奥威尔的传世作品，在这两篇小说中，奥威尔以敏锐的目光观察和批判以斯大林时代的苏联为代表的、掩盖在社会主义名义下的极权主义；以辛辣的笔触讽刺了那泯灭人性的极权主义社会和追逐权力的人；而小说中对极权主义政权的预言在之后的五十年中也不断地被历史所印证，这两部作品堪称世界文坛政治讽喻小说的经典之作，其影响绝不仅仅局限于文学界。他在小说中创造的“老大哥”、“新话”、“双重思想”等词汇，被收入英语词典，而由他的名字衍生出的“奥威尔主义”、“奥威尔式的”等词汇甚至成为通用词汇而广泛使用，由此可见奥威尔和他的作品在英语国家的巨大影响。<br />
<br />
　　由于历史上东西方的对峙，乔治・奥威尔的作品经常被视为反苏和反共的代名词，因而在苏联东欧等社会主义国家遭到封杀。《一九八四》直到1985年才有简体中文版刊行，而且标明“内部读物”只允许特定人群购买阅读。<br />
<br />
<strong>书籍简介：</strong><br />
<br />
　　《一九八四》（英文：Nineteen Eighty-Four）是英国作家乔治・欧威尔创作的一部政治讽刺小说，初版于1949年，与1932年英国赫胥黎著作的《美丽新世界》，以及俄国扎米亚京的《我们》并称反乌托邦的三部代表作，在这部作品中，欧威尔深刻分析了极权主义社会，并且刻划了一个令人感到窒息和恐怖的，以追逐权力为最终目标的假想的未来社会，通过对这个社会中一个普通人生活的细致刻画，投射出了现实生活中极权主义的本质。<br />
<br />
　　《一九八四》已经被翻译成至少62种语言，而它对英语本身亦产生了意义深远的影响。书中的术语和小说作者已经成为讨论隐私和国家安全问题时的常用语。例如，“欧威尔式的”形容一个令人想到小说中的极权主义社会的行为或组织，而“老大哥在看着你”（小说中不时见到的标语）则意指任何被认为是侵犯隐私的监视行为。《一九八四》曾在某些时期内被视为危险和具有煽动性的并因此被许多国家（不单是有时被视为采取“极权主义”的国家）列为禁书。本书被美国时代杂志评为1923年至今最好的100本英文小说之一，此外还在1984年改编成电影上映。<br />
<br />
　　《一九八四》被广泛认为是欧威尔的代表作，不仅文中的思想在西方社会产生了很大的反响，其中的语言也得到了广泛的认可。欧威尔在本文创造的一些新词，例如“思想犯罪”（thought-crime）、“新语”（newspeak）、“双重思想”（doublethink）、“老大哥”（Big Brother）等已收进词典。<br />
<br />
<strong>花絮：关于书名</strong><br />
<br />
　　最初欧威尔将小说命名为“欧洲的最后一个人”（The Last Man in Europe），但是他的出版商，Frederic Warburg出于营销需求建议他换一个书名。欧威尔没有反对这个建议，但他选择1984这个特别的年份的原因并不为人所知。也许他将他写作这本书的那一年（1948年）的后两位数颠倒过来，成为了可以预见的未来的1984年。他也可能借此暗指费宾协会（一本社会党组织，创立于1884年）建立一百周年。此外，他亦可能暗指杰克・伦敦的小说《铁蹄》（其中一个政治势力于1984年登上权力舞台）、G. K. Chesterton的《the Napoleon of Notting Hill 》（亦设定在1984年）或者他的妻子（Eileen O'Shaughnessy）的一首诗，诗名为“本世纪的终点，1984”。关于书名的最后一个猜测是欧威尔原本准备的书名是1980，但是由于疾病，小说的完成变得遥遥无期，因此他感到有必要将故事推入更远的未来。<br />
