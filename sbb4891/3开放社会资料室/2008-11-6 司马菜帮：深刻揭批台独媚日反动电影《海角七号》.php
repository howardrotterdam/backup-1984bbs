													<h2>司马菜帮：深刻揭批台独媚日反动电影《海角七号》</h2>
						
						
																											<div id="postmessage_3559" class="t_msgfont">看中影集团怎样引进媚日台片《海角七号》？<br />
<br />
作者：司R平邦<br />
<br />
如果能把铺叙间隔五六十年的台湾与日本两代青春男女的恋爱电影引起中国内地，或者还真是中国电影的一大希望DD淡化意识形态的信号到来了。 <br />
　　 <br />
　　但是，我相信《海角七号》即使在内地上映，它也不会如在台湾一样获得万口一辞的好评，相反，魏德圣导演或者可能步去年李安导演后尘DD《海角七号》也可能与去年的《色。戒》一样成为内地文化评论的最焦点之一。 <br />
　　 <br />
　　说实话，我看了这部被许多人包括许多内地白痴影评人厚爱的《海角七号》后，觉得很不舒服。 <br />
　　 <br />
　　而且，现在早就过了那种大家都说好，我也一定要说好的时间，对这个《海角七号》我也没办法跟着一样说好。 <br />
　　 <br />
　　有哥们儿看完它后给我发短信说：这是部“台独电影”。 <br />
　　 <br />
　　这个帽子很大，要慎重。 <br />
　　 <br />
　　其实，看后我觉得它并没有太明确的“台独”意识，谈不上（也可能是我在网络上看的版本质量太差，丢了很多内容吧）。 <br />
　　 <br />
　　一个二战后不得不回到日本的日本男人，在回国的船上给他留在台湾南部恒春的中国女人友子写了7封信，之后两个人一别无音讯，这个日本人的信也没有发出，直到2000年之后，当他死后他的孙辈把这些信捆好按原址寄回台湾，但信封上的那个“海角七号”的地址早找不到了。 <br />
　　 <br />
　　“海角七号“在哪里呢？ <br />
　　 <br />
　　电影名是《海角七号》，用意也在这个“海角七号”。 <br />
　　 <br />
　　什么是“海角七号”？ <br />
　　 <br />
　　它是当年一个日本男人和一个中国女人的甜密，其实，这很容易让人想到它的指代就是1945年以前日据时代的台湾，以及某些现在的台湾人对此的怀念DD虽然它不是我们这个大一统语境下的中国人所能接受的，甚至是憎恶的，或者这部电影表明现在确实有一些台湾人对那个时代怀着眷恋。 <br />
　　 <br />
　　从台北扫兴而回的主唱阿嘉（范逸臣饰）接替爷爷当了业余邮差，正接到了这封厚厚的寄自日本的信，他好奇地打开信（台湾的邮差真的很逊），读到了五六十年前的这个故事（那7封信），于是开始寻找曾经的“海角七号”和那个叫友子的台湾女人。 <br />
　　 <br />
　　友子，本是一个中国女人叫的日本名字，一听就知她是日据时代的日本文化教育下培养起来的一代台湾人，一个强烈的符号。 <br />
　　 <br />
　　为什么她不叫“淑珍”或者“秀莲”呢？ <br />
　　 <br />
　　电影至最后，阿嘉找到了那位友子，友子已经80多岁。 <br />
　　 <br />
　　但让阿嘉找到友子的人却是两个日本人，一个日本歌手，一个日本女导游，也叫友子（她的名字，其实是老友子名字来历的提示），阿嘉从台北回到恒春，组织了一支七拼八凑的乐队，是为了给那位著名的日本歌手的演唱会暖场，而在这个过程中，这个中国男人又爱上了中间人友子（田中千绘饰）。 <br />
　　 <br />
　　而关于五六十年前的那对日台恋人的恋情，电影没有交待，有的只是那个日籍老师的7封晦涩情书，电影更没有交待当年的那个友子在这样一段感情中的真实状况，是甜蜜？是艰难？是幸福？还是不幸？或者这段恋情本身就是一个大谎言？ <br />
　　 <br />
　　但在《海角七号》刻意营造的语境里，在现在的台湾电影人和影评人对此事的想当然想像中，这对恋人的感情堪比《泰坦尼克号》里的罗丝和杰克了，片中有大量的游船镜头进行这方面的暗示，把侵略者当年滚回本土的场面处理得堂皇优美；而这对日台恋人的旧情，其实只是被用来做烧酒以浇现在某些台湾人（文化矫情人）媚日情绪的块垒罢了。 <br />
　　 <br />
　　所以，这部电影至少让我知道台湾有3种人： <br />
　　 <br />
　　一种是支持统一的。 <br />
　　 <br />
　　一种是支持台独的。 <br />
　　 <br />
　　还有一种人是神往着台湾与日本重新走近的。 <br />
　　 <br />
　　电影中有一句话极具象征性，漂亮的日本女导游友子对新认识的台湾男朋友阿嘉说：“这回，不是我留下，就是你跟我走。” <br />
　　 <br />
　　上一次，因为二战失败，日本人被赶回了本土，那对苦命鸳鸯一生情缘当即了断，现在日台男女之间新的情缘出现了，或者是留下，或者是跟着走，他们谁也不想再被分开。 <br />
　　 <br />
　　什么是留下？什么是跟着走？ <br />
　　 <br />
　　有评论说这部电影胜在浓浓的本土情怀，但在我看来这不过是个托词，电影中阿嘉出场的第一句台词就是“我操你妈的台北”，如果用本土情怀来解释，也只能解释成是恒春对台北的叛逆DD引申一点是台湾本土意识对统一价值观的叛逆，“台北”的符号，既可以是扁政府时的台北，也可以是马政府的台北，但电影其实是用这句话引向所谓本土情怀里日台关系的紧密的历史关联。 <br />
　　 <br />
　　而且，其实，如果DD <br />
　　 <br />
　　将《海角七号》中大量的隐喻句子去掉DD试想如果内地电影局和中影集团最后通过这样一个版本的《海角七号》，这部电影的全部精彩与魅力至少要打掉五折。 <br />
　　 <br />
　　或者它又变得一无是处枯燥无味。 <br />
　　 <br />
　　那我们的电影管理和引进部门还要引进它干什么呢？ <br />
　　 <br />
　　看了这部电影，又看了些相关的评论，真的觉得中国电影根本没有希望，就这样一部与“我们“的主流价值观相悖的电影，仍然得到内地媒体、影评人和电影工作者们一边倒的支持，匪夷所思之极。而我也不相信这部电影里的艺术魅力可以和政治诉求完全分开，我说了，如果这部《海角七号》离了它精致、巧妙、尖锐的政治诉求，就是一滩狗屎。</div>

							
							
							
							
