黑暗中的天光 <br />
<br />
卢巴索夫望著窗外，融化的雪已经凝结，从机关枪碉堡那边往上看是一片银色的天。他开口说「人道主义与政治无法相容，尊重个人与社会进步无法相容。甘地是印度的祸害，追求手段的纯洁只会造成政治的无能，这些我都承认。可是，另一条道路又引导我们走向何处 <br />
