缄默的夥伴 <br />
<br />
卢巴索夫的狱室思索，有了意外的发现。<br />
他发现所谓的「独白」，其实是一种特殊的「对话」…对话的一方是缄默的，另一方则违反文法的规则，称对方为「我」而非「你」，目的在取得对方的信任以探察其意向。缄默的一方却一直保持沈默。<br />
缄默的一方有时也会突如其然地开口…卢巴索夫只看到见自己嘴唇在动，很讶异地细听那个完全陌生的声音… 他开始相信在「第一人称单数」中，隐藏著另一个实体，长期来保持沈默，现在才开始讲话。<br />
卢巴索夫仔细研究这个新发现个体的称之为「文法上的虚构」。它的思想，似乎由一些范畴凌乱而毫无联系的部份组成，譬如圣母像上交叉的手,小罗威的猫,教堂诗歌的音调,女秘书阿洛瓦说过的一句话,作回想过去的白日梦时所引发的昏眩状态等。它显然是人的重要的一部份，但又非其逻辑思考所能触及，而且往往趁人不备时发动袭击，使之牙痛，或陷入白日梦。卢巴索夫就因此回想起已被处死的阿洛瓦…<br />
他那时因口述疲倦而在室内徘徊…室内静静地充满她可亲的香味…卢巴索夫自己也不明白为什N会停在她的椅后，把双手轻放在她肩上，问她是否愿意晚上同他出游。她并未表示惊异，两肩仍然不动由他倚靠，静静地点头，甚至没有回首。<br />
当晚卢巴索夫笑著跟她说，「我那时以为你还在速记呢」。她那大而线条分明的胸部轮廓在黑暗中浮现，服贴柔和宛如一向就在那Y。「你永远可以要怎样对我就怎样对我」…这句话就像圣母像上那交叉的手，一直留在卢巴索夫的脑中。<br />
