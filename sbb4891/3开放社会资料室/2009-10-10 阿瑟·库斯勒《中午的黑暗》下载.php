回狱室的途中，卢巴索夫想起格勒金毫无表请的面部-代表国家的冷酷。这个国家靠著许多卢巴索夫,伊凡诺夫才能生存到今天。格勒金是他们的精神后裔，「这群新原始人种只是执行我们上一代未完成的工作」，卢巴索夫自言自语。<br />
