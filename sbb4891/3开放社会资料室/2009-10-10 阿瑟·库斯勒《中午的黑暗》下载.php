「那又怎N样 」 伊凡诺夫说「难道我们要因最终结果无法预见，就束手不动吗 每年有几百万人毫无意义的被病疫与天灾所杀害…没有人问他们为什N死。我们不过枪杀了几千个客观上有害的人，人道主义者就对我们谩骂。是的，我们肃清了农民中的寄生阶级并听任他们活活饿死。但这只是外科医生施行手术，终归是要作的。革命前的所谓好日子，一遇旱年也同样饿死那N多人，只是更无意义更无理由而已。中国黄河水灾的受害者有时数以几十万计。自然时常把人类作无意义的实验;人类为什N就无权把自己作实验 」<br />
伊凡诺夫站起来。「明天时间就到了，你我都需要清醒的头脑来策划口供。不要耸肩膀，你至少有一半相信自己会签字。如果你拒绝，那只是因为道德上的懦弱，道德懦弱驱使许多人变成殉道者」。<br />
伊凡诺夫将门反手关上时，卢巴索夫自知已经一半屈服了。他往床上一躺，虽然疲倦已极，却奇怪地有轻松之感。他像是被人掏空抽光，又像是如释重负。鲍可洛夫的悲号已在他的记忆中失去了尖锐性。效忠於活人，而非效忠於死人，谁能说他是出卖呢？<br />
