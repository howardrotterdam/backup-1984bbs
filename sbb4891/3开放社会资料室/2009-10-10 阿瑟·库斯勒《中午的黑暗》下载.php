有缺点的罗盘针 <br />
<br />
卢巴索夫晓得自己在午夜之前将不复生存。<br />
他站在窗前，额头倚著窗格，对面的机关枪堡垒后面现初一片蓝天，使他忆起童年时躺在草地上看白杨树缓慢晃动时索看到的那片晴空。真的，有时一片天空也可以使人陷入心理学家称为「大洋感」(oceanic sense) 的那种恍惚状态。在这种状态下，个人就像大海中的一粒盐渐渐溶化了，而无垠无际的海也像是包容在这一粒盐里。没有时间与空间的限制。思想失去了方向而开始打转，像是罗盘针走近磁性的两极，脱离了轴心而到处荡动。<br />
四十年来他始终遵循逻辑的法则，拒绝「缄默夥伴」的诱惑，并尽力对抗那种「大洋感」。回顾以往，自己四十年来像是在发狂，发纯理性的狂。<br />
窗外已是黑夜。很快就要结束了。可是问问自己「你为何而死」 仍无解答。<br />
制度一定有错误;也许错误就在那项理论里。过去他深信不疑「但求目的不择手段」;这句话消灭了革命的伟大友爱，而使革命者完全发狂。自己的日记中不是写著吗「我们已抛弃一切规范，唯一可遵循的是必然性的逻辑。我们有如航行大海，却无伦理上的压舱物」。<br />
也许问题就在那里。也许人类不适宜作没有压舱物的航行。也许理性是有缺点的罗盘针，引人走著弯曲的路，以致在雾中迷失了目标。<br />
<br />
鼓声若响<br />
 <br />
402号传来「他们正在抓缺唇。他向你致意」<br />
所有的囚犯站在各自门前，双掌有节奏地敲著铁门，像仪仗队似的击鼓送别。脚步声由远而近从走廊上传来，缺唇突然出现在门洞的视线内，带著手铐，双手被扭绑到背后，很快地走出卢巴索夫的视界外。<br />
鼓声消失，一切复归静寂。<br />
「大概还有十分钟，你怎样感觉」 402号知道缺唇与卢巴索夫同案，下一个就轮到他了。<br />
「我希望业已完事」;卢巴索夫心里感激，知道402号是故意同他讲话，让他容易渡过时间。<br />
「你还记得吗雪白的乳部像香槟酒杯 哈哈!你真可恶…我给你一点内行的建议把膀胱弄空…精神虽然坚强，肉体是软弱的…哈哈」!<br />
卢巴索夫微笑，顺从地走到马桶边…事后他回答「多谢。好劝告。你呢 」<br />
402号有几秒钟没出声。旋而缓慢地回答「还有十八年。正确的说，应该是六千五百三十天…我真羡慕你。你想吧- 还有六千五百三十夜而没有一个女人」。<br />
忽然，他大声急忙地敲打「他们来了…」。过了几秒钟又补充依句「真可惜。我们刚刚要高兴的谈下去呢…」。<br />
卢巴索夫从床上站起身，想一想，又敲「你帮忙我很多。谢谢」。<br />
<br />
永恒的波浪 <br />
<br />
走廊的鼓声又响起来。鼓声随著他走过每一扇铁门，走过通往地窖的螺旋形台阶，他的夹鼻眼镜不慎掉下来，玻璃裂碎，镜框滚到下面。卢巴索夫听到后面的人弯身拾起眼镜放入口袋，但他始终没有回头看。<br />
前面是条长廊，走了几分钟。卢巴索夫设法想一些别的事，集中全力阻止自己回头。<br />
奇怪的是，在开庭后幸福的寂静包围他的时刻起，他的牙痛停止了，也许脓肿就是在那时破了头。他对他们怎么说的？“我在国家，群众，全体人民面前跪下双膝……”那又怎么样？这些群众，这些人民后来怎么啦？四十年来他们用威胁和允诺，用幻想的恐怖和幻想的酬报把人民赶过沙漠。但是许诺的乐土在哪里？<br />
流浪的人类真的有这样一个目的地吗？这是个他想在还来得及的时候得到答复的问题。摩西也没有让进入这个许诺的乐土。但是让他从山顶上看到了它，就在他的脚下。这样，在自己的眼前确实看到了自己的目的地，死也就容易了。他，尼古拉斯・萨尔曼诺维奇・鲁巴肖夫却没有给带到山顶上，他的眼睛不论朝哪儿望去，都看不到什么，除了沙漠和夜晚的黑暗。<br />
忽然，他脑后受到麻痹性的一击。他早就预料到了，却仍在他没有提防的时候来到。他感觉讶异，双膝下屈，身子打了半转摔倒…脸伏在冰冷的石板上。<br />
四周漆黑，海水把他冲到唱著夜曲的水面。记忆在他面前转，像是细蒙的水花。<br />
大门外有人敲门，他正做著他们来逮捕他的梦…他竭力想把手臂伸入睡衣的袖管…一个无形的影子像他弯下身子，他嗅到枪带上发出的生革味，但是那个人影的袖上和肩上佩的是什么徽章――它以谁的名义举起手枪的黑黝黝的枪膛？<br />
<br />
<br />
全书最后几句话，版本一：耳上又受了猛烈的一击。一切安静下来，只剩下海洋的声音。远方打过来一个波浪,将他慢慢抬起，又缓缓的离去，永恒不过如此。<br />
<br />
版本二：第二下摧毁性的打击落在他的耳朵上。接着一切寂静无声。又是海和海涛声。一阵波浪涌来把他慢慢托起。这是永恒的一耸，它从很远很远的地方来，又继续安详地向前逝去。<br />
<br />
