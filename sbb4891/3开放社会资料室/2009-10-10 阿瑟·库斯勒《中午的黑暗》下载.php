「你很了解这一点，也晓得其中的利害关系，但却向我提到鲍可洛夫的啜泣…假使我们坚持「每个人都是神圣不可侵犯」的人道主义，认为不应以数学原则来看人的生命…那岂不是说营长不应该牺牲一队人来营救一团人，也不应牺牲鲍可洛夫那种傻子，而听任海军走入歧途吗 」<br />
