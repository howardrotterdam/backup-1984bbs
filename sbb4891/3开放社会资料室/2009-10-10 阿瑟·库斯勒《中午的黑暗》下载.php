相对性成熟律 <br />
<br />
卢巴索夫入狱后第廿日的日记：<br />
「历史的钟摆从专制摆荡到民主，又从民主摆荡到专制独裁。<br />
「一个民族所能保有的个人自由，完全看这个民族的政治成熟度而定。上面所说的钟摆荡动暗示群众的政治成熟度，并非是一直往上升的，而由较复杂的原理所支配…一个民族是否能照民主的方式管理自己，与这个民族对整个社会机构的组织与运作了解的程度成比例。<br />
「技术每次进步的跳跃，往往使群众一时无法了解…相对的政治成熟度就下降，有时需要十年，有时需要几代…才能逐渐恢本身自治的能力。<br />
「群众意识的水准一旦赶上客观情势后，民主主义就必然胜利…胜利一直维持到技术文化再度跳跃时-例如纺织机的发明-群众又退后到较不成熟的阶段，专制独裁应运而生，甚至成为必要。<br />
「这种过程就好像用一个有几层渠道的水闸来抬起轮船。船进入第一层渠时，因水面个加高而慢慢升到最高点;但到了第二层时，一切过程又必须从新开始。每一层渠的四周围墙代表技术文化;渠内的水面则代表群众的政治成熟程度。只看渠内水面高於海面并无意义，问题在於水闸内水面相对高低的比较…因此可以了解廿世纪上半期欧洲诸国的政治成熟程度，为何不及纪元前两百年的政治成熟程度。<br />
「革命发源地(苏联)虽然已达第二层船渠，但仍处於这新的一层的最低地带…必须重新经历费力而痛楚的过程…」<br />
卢巴索夫想起有次读到关於新几内亚岛上土人生活情形的论文…他们显然已经到达「底层船渠的最高水面…」，而能获得社会协调的生活，建立了初期第好的民主机构。卢巴索夫相信自已发现了「相对性的成熟律」。<br />
