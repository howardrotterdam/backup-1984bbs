达明一派<br />
<br />
1962年6月16日黄耀明出生。1963年2月27日刘以达出生。<br />
<br />
1985年，刘以达在现已停刊的《摇摆双周刊》上刊登广告寻找合作伴侣，黄耀明应征，两人一拍即合。资深广播人俞b为他们取名“达明一派”，并介绍他们入宝丽金唱片公司。<br />
<br />
1986年，三月推出第一张EP《达明一派》，九月推出第一张大碟《达明一派Ⅱ》。<br />
<br />
1987年，四月推出大碟《石头记》，此碟乃公认是达明最经典的作品。同年十二月推出大碟《我等着你回来》。<br />
<br />
