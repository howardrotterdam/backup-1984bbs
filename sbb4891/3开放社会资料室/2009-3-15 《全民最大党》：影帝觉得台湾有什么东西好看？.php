													<h2>秀莲姐也回应：很想到大陆看看</h2>
						
						
																											<div id="postmessage_54022" class="t_msgfont">● 沈泽玮 台北特派员<br />
　　她的装扮并不新潮，发型还是担任副总统时的那样，没有刻意求变，但却看得出是认真打理过的。身上穿的是单色调的粉红套装，配上一副珠链，经过八年作为经常出现在媒体上的公众人物，这个形象，似乎也已经让人们习以为常是“吕秀莲风格”了。<br />
<br />
　　脸上的彩妆，颜色对比分明，但与流行无关。腮红之外，绿色的眼盖膏，尤其突出，仿佛提醒注视她的人，她属于民进党的开创功臣一代。她脸上表情随着课题的转换而转换。<br />
<br />
　　这就是快人快语的台湾前副总统吕秀莲。<br />
<br />
　　上周在位于卸任后的办公室接受本报专访时，65岁的吕前副总统从319枪击事件、阿扁总统案件、办报纸、访不访大陆、民进党的未来等课题侃侃而谈。对参与推动台湾民主的成就，她更是自豪，激动时还拍桌。<br />
<br />
　　她乐于面对记者，但也随时掌握主导权，如果不想记者再问，她会毫不掩饰地收起笑容，然后说明：“这个部分，我就不再回答。”<br />
<br />
　　两岸最近热炒领导人及前领导人要到对岸访问的新闻，台湾媒体也诸多报道吕秀莲是否前去大陆访问的信息。<br />
<br />
　　好奇地问当事人是否有意愿跨越海峡，她没有明说，不过讲着讲着，她也透露心声，“真的很想去看”。<br />
<br />
　　当中国国务院总理温家宝在两会记者会上说，爬都要爬去台湾之前，吕秀莲在一次访问中就表示，如果获得邀请，她愿以《玉山午报》创办人的身份访问大陆。<br />
<br />
　　在两会期间，政协台联小组委员会杨思泽释出了信息，欢迎吕到大陆去。如果成行，她将是首位访问大陆的卸任民进党籍副总统。<br />
<br />
吕秀莲告诉记者，她注意到，北京有意愿和诚意欢迎她去，她体察到中国的善意了。<br />
　　她说：“我去，最大是希望认识现在崛起的中国，那有机会我也希望代表台湾广大的人民，希望怎么样来改善和中国的关系，可以有正面对谈当然是很好啊。那，其实我真的很想去看。现在有120万台湾人在大陆，我去看看他们在大陆的实际情况。因为台湾的部分媒体是报喜不报忧。有机会我去看看台商在那边做的事情。”<br />
<br />
　　至于身份问题，她表示，不管用什么身份去，她“就是台湾做过八年的副总统”。不过，她也强调，一定要对等和尊严，如果用报纸的名义，她需要取得《玉山午报》董事的同意，另外她还是民进党党员，也会和民进党沟通。另一个前提是，西藏不能有压迫人权的事发生，因为保护人权是她的信念。<br />
<br />
吕秀莲登陆<br />
<br />
最大阻力来自独派<br />
<br />
　　外界判断，登陆一事，吕秀莲面对最大的阻力将来自独派。<br />
<br />
　　其实，早在1990年，主张台湾独立的吕秀莲就已经到过大陆。她说，当时她就“觉得台湾的前途不能自外于中国”，因为知道隔年就要选立法委员，她当时觉得去了解中国会对自己的立委工作有帮助，所以就以自由身登陆。<br />
<br />
　　她回忆道：“那时候我跟当时的统战部代理部长万绍芬女士谈了三个小时，我们都很认真地谈问题。他们安排我去西安、重庆，然后到福建厦门回来。那个旅行是很重要的，我觉得我对中国有个初步的了解，当然现在，一转眼快二十年过去了。我当然觉得在适当的时候可以去，也很想去。”<br />
<br />
　　吕秀莲承认，民进党对中国大陆的现状了解不够多，国际观可能也不够，如果要知己知彼，就有必要要做好更多的准备。<br />
<br />
台湾和中国大陆就是“远亲近邻”<br />
　　对于台湾和中国大陆的关系，她重申，就是“远亲近邻”，但问题就在于怎么去落实这样的的关系。<br />
<br />
　　她说：“的确啊，我们的祖先是来自中国啦，可是这几年为什么对台湾这么不好？大家检讨一下嘛。台湾不可能危害中国，根本不需要打仗。为什么不能帮助台湾在国际上？像最近3月份，在联合国有个妇女会议，我们也有妇女团去啊。它说，只要是台湾来的，一律不准进去。这个很过份。”<br />
<br />
　　顺着讲到世界卫生大会（WHA），吕秀莲呼吁大陆大方一点，不要“小鼻子小眼睛”，WHA台湾早就该参加了。她说，台湾不是什么trouble maker（麻烦制造者），台湾还可以分享在沙斯和禽流感的经验。<br />
<br />
　　台湾可不可以加入WHA，5月份会有答案。而更快迎来的倒是3月19日，震撼全台湾社会的大选前夕离奇枪击案五周年。提到319，就不得不提及吕秀莲过去八年的搭档陈水扁。当时，她的膝盖受枪伤，陈水扁则是腹部受伤。<br />
<br />
　　为了解开种种疑团，力求当局再次查明真相，吕秀莲上周出版了《透视319》新书。<br />
<br />
　　她在书中提出了四个可能：红（大陆）、蓝（蓝营）、绿（绿营）、黑（黑道）。受访时，她表示从排除法推测，黑道暗杀的可能性最大，因为涉及了赌盘。<br />
<br />
　　至于有媒体强烈质疑的第五种可能――阿扁自导自演，吕秀莲认为不可能。<br />
<br />
　　她笃定地说：“胡说八道，我书上都论述了，你可以去看。那他那个伤的确是枪伤，不是刀伤。更重要的是，那天早上9点钟，我跟他一路都在一起，我们除了上厕所两三分钟洗手没有在一起以外，他没有可能有时间去搞这个事情。”<br />
<br />
　　经历八年的风风雨雨，吕秀莲以“一言难尽”来形容在位的日子。<br />
她承认自己不甘寂寞，也不甘被媒体称为“深宫怨妇”，因为她是经过两次选举，得到选民两次肯定的。虽然受到副总统虚位的限制，但是她强调，自己在授权的范围内，已经做了许多对得起台湾的事。<br />
　　她说，921地震，她以副总统的身份到灾区65次；她还照顾台湾的弱势团体；外交，她出访12次；她还创办民主太平洋联盟。<br />
<br />
　　八年的副总统生涯，她以宋朝诗人杨万里的《桂源铺》诗句来总结。<br />
<br />
　　“万山不许一溪奔，最后我还是堂堂溪水出前村。就是在制度面不允许之下，我还是努力去做我认为应该做的，那就是良心嘛。”<br />
<br />
（二之一）</div>

							
							
							
							
