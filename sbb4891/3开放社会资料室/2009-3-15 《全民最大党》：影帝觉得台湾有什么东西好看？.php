																											<div id="postmessage_53951" class="t_msgfont"><a href="mms://media.xinhuanet.com/media2/guoneixinwen/090315094931_news_fm_19.wmv" target="_blank">mms://media.xinhuanet.com/media2 ... 4931_news_fm_19.wmv</a><br />
<br />
&nbsp;&nbsp;新华网台北３月１６日电（记者李凯　王妍）１６日上午８时３０分许，由港中旅集团组织的“安利心印宝岛万人行”首发团１６００人，乘坐“海洋神话号”豪华邮轮抵达台湾基隆港，这是自去年７月大陆居民赴台旅游实施以来规模最大、人数最多的旅游团，也是两岸“三通”基本实现以来第一个邮轮直航团。<br />
<br />
基隆港派出前导船喷洒三支水柱，以最高规格礼仪欢迎远道而来的嘉宾。在码头上举行的盛大欢迎仪式上，祥龙瑞狮和极富台湾民间特色的“八家将”齐舞，鼓乐齐鸣，岸上的欢迎民众和船上的游客相互招手欢呼，“欢迎你”呼声此起彼伏。 <br />
<br />
&nbsp; &nbsp; 基隆市市长张通荣以及台湾的旅游、交通等部门的官员参加了欢迎仪式。 <br />
<br />
&nbsp; &nbsp; “海洋神话号”是１４日由上海出发经过近４０小时的航行抵达基隆的。 <br />
<br />
&nbsp; &nbsp; 据介绍，安利此次１．２万人包乘豪华邮轮赴台旅游，将分９个批次，每航次历时６天７夜，分别停靠基隆、花莲、台中等港口，游览台北故宫、基隆夜市、太鲁阁、日月潭等著名景点，并将品尝台湾美食，感受台湾风情。 <br />
<br />
&nbsp; &nbsp; 安利公司大中华区行政总裁颜志荣表示，来台访问的是公司的精英职工，大家几乎都是第一次来台湾旅游，对于能够亲自体验憧憬已久的宝岛美景、美食以及风土人情，都倍感幸福。 <br />
<br />
&nbsp; &nbsp; 张通荣对大陆游客的到来表示欢迎。他说，在这春暖花开的季节，有朋自远方来，也带来了两岸美好的愿景，希望大陆朋友台湾之行喜悦、顺遂、愉快。<br />
