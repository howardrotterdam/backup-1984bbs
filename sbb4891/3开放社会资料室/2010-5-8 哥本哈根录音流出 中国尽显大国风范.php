<br />
来源：人民网<br />
<br />
　　国务院今天召开全国节能减排工作电视电话会议，动员和部署加强节能减排工作。国务院总理温家宝作了重要讲话，他强调，要切实把节能减排作为加强宏观调控、调整经济结构、转变发展方式的重要任务，本着对国家、对人民、对历史高度负责的精神，下更大的决心，花更大的气力，做更大的努力，确保实现“十一五”节能减排目标。（5月5日新华社）<br />
　　节能减排是我国“十一五”规划提出的重大战略目标。《中华人民共和国节约能源法》所称节约能源，是指加强用能管理，采取技术上可行、经济上合理以及环境和社会可以承受的措施，从能源生产到消费的各个环节，降低消耗、减少损失和污染物排放、制止浪费，有效、合理地利用能源。节能减排就是节约能源、降低能源消耗、减少污染物排放。我国《国民经济和社会发展第十一个五年规划纲要》进一步明确提出，“十一五”期间单位国内生产总值能耗要降低20％左右，主要污染物排放总量要减少10％的，这是一组约束性指标。根据这两个指标，如中国GDP年均增长一成，五年内就需要节能六亿吨标准煤，减排二氧化硫六百二十多万吨、化学需氧量五百七十多万吨。可以说，这是党和政府提出的重大战略目标，对人民作出的庄严承诺。<br />
　　“十一五”规划实施以来，我国节能减排工作取得了重大进展。党中央、国务院以科学发展观为指导，“十一五”规划实施以来，对节能减排工作做出了一系列强有力的决策和部署，各地区、各有关部门采取更加有力有效的政策措施，比如有的地方实行能耗总量控制、更加明确节能减排“一票否决”的具体内涵，使节能减排放的位置进一步突出，推动力度进一步加大，政策措施进一步落实，社会氛围进一步浓厚，节能减排效果进一步显现，全国单位国内生产总值能耗累计下降14.38％。可以说节能减排成效显著，来之不易。但这一14.38％的下降率与“十一五”降低20％左右的目标仍有较大差距，特别是去年三季度以来，高耗能、高排放行业快速增长，一些淘汰的落后产能死灰复燃，能耗强度下降趋势减缓甚至由降转升，节能减排形势十分严峻，全面完成节能减排“十一五”目标任务依然十分艰巨。<br />
