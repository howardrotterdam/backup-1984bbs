2000年，世界末日没有到来，我住在树村。那时不好意思跟人说，我是搞民谣的，在血气方刚的摇滚斗士中，民谣意味着软弱、矫情、不痛不痒。<br />
但也有一次例外，我和几个金属朋克朋友去五道口的橡树酒吧看“野孩子”乐队的现场，门票10元，还送一杯扎啤。第一次看他们的现场，真是牛，一曲连着一曲，好像被雨水冲刷过的绿色的火车，咯噔噔的从你身上开过去，又美好又有力。跟我一起的那几个哥们都被镇了，说在北京从来没看过这样好的现场。后来 “野孩子”主唱张缧∷鳎开了“河”酒吧，各路潜伏于地下的牛鬼蛇神云集响应，来看演出的八成都是歌手或者乐手，所以经常会发生这样的事情：台上一个人唱的刚刚性起，底下迫不及待的蹦上一个打手鼓的，接着手风琴上去了、冬不拉、曼陀琳、萨克斯一拥而上，最后就变成了交响乐。<br />
小河的《飞的高的鸟不落在跑不快的牛的背上》，还有万晓利的《《走过来 走过去》，还有王娟、马木尔的IZ的第一张小样都录制于“河”，虽然不够高保真，但那种充沛的激情，年轻的锐气，是后来在精确的录音棚里所难以复制的。<br />
04年10月小索去世了，一场新世纪的另类大爬梯酒阑人散了。对于新民谣，河酒吧就是一次小当量的核聚变。我们在爆炸的阵痛中，逃往四面八方，逃向自己的土地，生根发芽称王称霸。<br />
接过“河”酒吧衣钵的是开在亚运村的“无名高地”酒吧，空旷的场地，幽暗的灯光，老鼠在木质舞台下蠢蠢欲动，蟑螂在桌子上爬来爬去。每周二、三有民谣演出，免门票。据罗永浩回忆，他那时每周三必去高地看小河的演出，从海淀农大到亚运村风雨无阻；绿妖回忆，那时她作为者名玉米，有一次带领众多玉米看周云蓬的演出，结果，我被排在第18个上场，一直等到凌晨2点；据我来我征服回忆，他们那时找房子唯一的标准就是有没有直达高地的公共汽车。<br />
07年，4月30日，我在“无名高地”最后一次演出，不久的将来，它和“河”酒吧一样因亏空而关门。我带了刚刚出厂的《中国孩子》唱片，我说，这是我自己做的，刚刚出炉，还热乎呢，谁买呀？当晚，卖了18张，大卖呀，高兴的不得了。<br />
新民谣正在长大。05年小河在798策划了一场民谣音乐会，第一次冠名为新民谣，区分于当年的校园民谣。06年，张晓舟在深圳体育馆组织“重返大地――2006中国民谣音乐周”。07年，“迷笛”音乐节设立民谣舞台，人气和主舞台不相上下。同年，万晓利获得华语音乐传媒大奖“最佳民谣艺人奖”，打破了港台艺人对此项奖的垄断。<br />
有些人走向更大的舞台，有些人走向更深远的土地和内心。<br />
野孩子主唱张纾定居在云南，他把自己的音乐简约成一把冬不拉和一个口琴，仿佛高手练剑,一花一草皆为利器。<br />
小河铺排开他的笔记本电脑，还有五花八门的效果器，他体内汹涌碰撞的多道真气，终于找到了宣泄的河床，他的一个人的交响乐，繁复成张绲牧硪桓黾端。<br />
苏阳离开了金属风格的“透明乐队”，他加入了一个秦腔剧团，到乡下的庙会上去演出。他去固原，找唱歌的回民老马，学习唱“凤凰”。.<br />
王娟厌倦了自己清纯女生的角色，和电子音乐家虎子，共同录制了《两个人的旅行》。<br />
小娟和她温馨的小团队，不再满足于月亮度假村的驻唱生活，频频参加原创现场，以她天籁般的声音，迅速崛起。<br />
李志把“梵高先生”传到网上，不凭借主流媒体的追捧，一个人单枪匹马，一路杀出，打下了新民谣人气王的家天下。<br />
新民谣的兴起，并非是小贫乍富，它更像是蚂蚁搬家，一点一滴日久见人心。它得力于互联网的自由传播，人们对于宣泄自己心理诉求的渴望，以及平易近人的现场音乐的回归，仿佛多年前的天桥撂地，梨园捧角儿。<br />
只看09年的演出纪录，风头正劲的第一梯队张纭⑾利、苏阳、李志的巡演路线覆盖全国，许多人的巡演日程安排到了12月31日；而新锐部队白水、五条人、刘2也当仁不让，南北穿插，常有在同一个城市，两场民谣演出撞车的尴尬局面；还有小河欧洲30场演出，美国20多场演出。小娟在台湾、赵老大和马木尔在香港、可谓遍地开花。<br />
09年，为帮助贫困盲童筹款的民谣唱片《红色推土机》首印3000张，已全部卖完，感谢这些民谣艺人无偿的录制，已有19个盲童于此项目受益。我们准备再制作3000张唱片，是《红色推土机》的升级版，新加选了杭盖乐队和宋雨哲的新歌。<br />
