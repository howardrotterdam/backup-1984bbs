																											<div id="postmessage_647412" class="t_msgfont"><img src="http://yopic.us/thumbs/5201052959.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
美国《中国文化大革命文库光碟》编委会 及&nbsp;&nbsp;<br />
香港中文大学中国研究服务中心合作编撰 <br />
宋永毅 主编&nbsp;&nbsp;<br />
英文名称：Chinese Cultural Revolution Database<br />
发行时间：2002年<br />
出版单位：中文大学出版社<br />
<br />
由宋永毅主编，美国《中国文化大革命文库》光碟编委会及香港中文 大学中国研究服务中心合作编纂的《中国文化大革命文库》光碟，在香港出版。据 编纂者称，光碟收集了文革文献上万篇，总数三千多万字，仅目录索引一书即近四 百页七十万字。文革结束已逾四分之一个世纪，有关文革研究的“原始的历史资料 和系统性的整理工作”仍然“双重缺乏”（宋永毅：《中国文化大革命文库》总导 言），编纂者经三年多努力，集成了迄今为止文革研究最大规模的资料库，个中辛 劳，可想而知。 <br />
<br />
一、有关文化大革命的中共文件、指示、公报汇编<br />
二、毛泽东关于文化大革命的讲话、指示和文章<br />
三、林彪关于文化大革命的讲话、指示和文章<br />
四、中央首长关于文化大革命的讲话和指示<br />
五、有关文化大革命的重要报刊社论文章<br />
六、文化大革命中红卫兵、群众运动重要文献<br />
七、文化大革命中的异端思潮重要文献<br />
<br />
<br />
总导言----宋永毅<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;二十世纪六十年代的中国文化大革命（1966－1976），不仅在中国当代史上，而且在整个世界史上也写下了极为重要，又极为复杂的一章。已故的著名汉学家费正清曾这样评价中国文化大革命的“惊人性”和“规模、影响与复杂性”：“无论从哪一个方面来看，这都是一个值得世世代代长期研究下去的历史课题”。诚如费正清所言，就文化大革命的影响与复杂性而言，它毫无疑问是“一个值得世世代代长期研究下去的历史课题”(China Watch, Harvard University Press, 1987, p. 158)。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;文革首先是一场历史悲剧。毛泽东和中共因为党内斗争而发动的所谓“革命”，使数以百万计无辜的中国人失去了他们的宝贵生命，又使数以亿计的普通百姓及其家庭创巨伤重。此外，文革使知识贬值，文明扫地；还使中华民族悠久的文化传统和文明惨遭破坏。从这个角度，中国作家巴金早在1986年就发出了建立“文革博物馆”，“有责任让子子孙孙、世世代代牢记十年惨痛的教训”的呼吁（《十年一梦》，人民日报出版社，1986，166 页）。可惜的是，由于种种原因，中国官方二十年来对这一呼吁一直噤若寒蝉，有意任其湮灭。在最应当建立“文革博物馆”的中国大陆，建立“文革博物馆”的呼吁却成了巴金可能终生都无法实现的梦。<br />
&nbsp; &nbsp; 文革又出乎意料地改变了当代中国的整个政治和经济面貌。当毛泽东和中共把他们原来就错误的政治和意识形态在文革中推向灾难性的顶峰后，稍有良知的人不难认识到那套“主义”的疯狂和荒谬了。这也就是连文革后的中共领导人都终于被迫走向改革开放的社会动因之一。至于文革所激发的对所谓“马列主义”和“毛泽东思想”的普遍怀疑和对大陆文革后民主运动的重要推动，又更是有目共睹的史实。从这一角度，文革在半个世纪以来的当代中国史上的地位和作用，更值得深入持久的认真研究。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;文革结束已经有近三十年了。作为一个历史学的研究课题，海内外的文革研究呈现出一种非常独特的景观。一方面是“说不尽的文革”，即文革成了一个国际性的持久的研究热点。据不完全统计，从1966年到1996年三十年内，全世界各种语言的研究文革的著作，就达七千本（篇）（参见 The Cultural Revolution: A Bibliography, 1966-1996, Cambridge, MA: Harvard-Yenching Library Bibliographical Series, Harvard University，1998）。另一方面却是“被禁忌的文革”，即在中国大陆它还是一个犯禁的独立研究课题。然而，大陆的学者和作家们仍以回忆录、报告文学等等形式书写文革。但是，无论是中国大陆的学者还是国际汉学界的文革研究都还存在不少困难。其中很重要的是原始的历史资料和系统性的整理工作的双重缺乏。文革中的官方和非官方的出版物可谓浩如烟海，但文革后中国图书馆中的许多这类收藏都无法自由查阅。另一方面，收藏在民间和海内外学者手中的不少可贵资源又因为缺少一个资料信息的共享工程，无法发挥它们的最大作用。由此，海内外文革研究中由史料问题引发的史实的错讹、真相的曲解和研究的空白仍普遍地存在，阻碍着这一重大研究课题的进展。<br />
&nbsp; &nbsp; 从总结历史教训，揭示历史真相，推动文革研究这一重大历史课题的目的出发，北美地区来自中国大陆的七位学者和一位台湾学者于1998年组成《中国文化大革命文库》光碟编辑部，开始了这一筚路蓝缕的浩大的史料编纂工程。很快，他们的工作得到了台湾、香港和大陆学者的大力支持。香港中文大学中国研究服务中心还成了合作单位。三年多来，编者们走遍了世界各地的亚洲图书馆，征集了众多的私人收藏，抢救了无数第一手的文字资料，在数以百万计的官方和非官方的文献中进行专业性的编辑校勘，汇集成了这一光碟中的上万篇文献。<br />
&nbsp; &nbsp;&nbsp; &nbsp; 汇编在这一光碟中的史料，限于第一手的文字资料。虽然也有一些文革后与文革密切相关的文献，但回忆录、访问记等等的二手材料均不在收录之列。就数量而言，已经有近三千万字之巨，共分为“有关文化大革命的中共文件、指示和公报”、“毛泽东关于文化大革命的讲话、指示和文章”、“林彪关于文化大革命的讲话、指示和文章”、“中央首长关于文化革命的讲话和指示”、“有关文化大革命的重要报刊社论文章”，“文化大革命中红卫兵、群众运动重要文献”和“文化大革命中的异端思潮重要文献”等七大部分。为了方便中外学者查阅，文库还采用了中英文两种目录。<br />
&nbsp; &nbsp;&nbsp; &nbsp; 如此巨大的史料容量，如果采用传统的印刷出版，可能有数百本之多，连存放都很不方便。而现代计算机技术却使它们能浓缩在一盘体积极小的光碟之中，而且还能够运用“作者”、“标题”、“时间”和“关键词”等等途径检索。从这一意义上而言，这个文库光碟正借助于现代科技，在史料汇编方面完成了一个研究性和资料性并重的“文革博物馆”。<br />
