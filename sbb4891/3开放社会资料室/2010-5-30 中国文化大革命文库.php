美国《中国文化大革命文库光碟》编委会 及&nbsp;&nbsp;<br />
香港中文大学中国研究服务中心合作编撰<br />
宋永毅 主编&nbsp;&nbsp;<br />
英文名称：Chinese Cultural Revolution Database<br />
发行时间：2002年<br />
出版单位：中文大学出版社<br />
<br />
由 宋永毅主编，美国《中国文化大革命文库》光碟编委会及香港中文 大学中国研究服务中心合作编纂的《中国文化大革命文库》光碟，在香港出版。据 编纂者称，光碟收集了文革文献上万篇，总数三千多万字，仅目录索引一书即近四 百页七十万字。文革结束已逾四分之一个世纪，有关文革研究的“原始的历史资料 和系统性的整理工作”仍然“双重缺乏”（宋永毅：《中国文化大革命文库》总导 言），编纂者经三年多努力，集成了迄今为止文革研究最大规模的资料库，个中辛 劳，可想而知。<br />
<br />
一、有关文化大革命的中共文件、指示、公报汇编<br />
二、毛泽东关于文化大革命的讲话、指示和文章<br />
三、林彪关于文化大革命的讲话、指示和文章<br />
四、中央首长关于文化大革命的讲话和指示<br />
五、有关文化大革命的重要报刊社论文章<br />
六、文化大革命中红卫兵、群众运动重要文献<br />
七、文化大革命中的异端思潮重要文献<br />
<br />
<br />
总导言----宋永毅<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;二十世纪六十年代的中国文化大革命（1966－1976），不仅在中国当代史上，而且在整个世界史上也写下了极为重要，又极为复杂的一章。已故的著名汉学 家费正清曾这样评价中国文化大革命的“惊人性”和“规模、影响与复杂性”：“无论从哪一个方面来看，这都是一个值得世世代代长期研究下去的历史课题”。诚 如费正清所言，就文化大革命的影响与复杂性而言，它毫无疑问是“一个值得世世代代长期研究下去的历史课题”(China Watch, Harvard University Press, 1987, p. 158)。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;文革首先是一场历史悲剧。毛泽东和中共因为党内斗争而发动的所谓“革命”，使数以百万计无辜的中国人失去了他们的宝贵生命，又使数以亿计的普通百姓及其家 庭创巨伤重。此外，文革使知识贬值，文明扫地；还使中华民族悠久的文化传统和文明惨遭破坏。从这个角度，中国作家巴金早在1986年就发出了建立“文革博 物馆”，“有责任让子子孙孙、世世代代牢记十年惨痛的教训”的呼吁（《十年一梦》，人民日报出版社，1986，166 页）。可惜的是，由于种种原因，中国官方二十年来对这一呼吁一直噤若寒蝉，有意任其湮灭。在最应当建立“文革博物馆”的中国大陆，建立“文革博物馆”的呼 吁却成了巴金可能终生都无法实现的梦。<br />
&nbsp; &nbsp; 文革又出乎意料地改变了当代中国的整个政治和经济面貌。当毛泽东和中共把他们原来就错误的政治和意 识形态在文革中推向灾难性的顶峰后，稍有良知的人不难认识到那套“主义”的疯狂和荒谬了。这也就是连文革后的中共领导人都终于被迫走向改革开放的社会动因 之一。至于文革所激发的对所谓“马列主义”和“毛泽东思想”的普遍怀疑和对大陆文革后民主运动的重要推动，又更是有目共睹的史实。从这一角度，文革在半个 世纪以来的当代中国史上的地位和作用，更值得深入持久的认真研究。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;文革结束已经有近三十年了。作为一个历史学的研究课题，海内外 的文革研究呈现出一种非常独特的景观。一方面是“说不尽的文革”，即文革成了一个国际性的持久的研究热点。据不完全统计，从1966年到1996年三十年 内，全世界各种语言的研究文革的著作，就达七千本（篇）（参见 The Cultural Revolution: A Bibliography, 1966-1996, Cambridge, MA: Harvard-Yenching Library Bibliographical Series, Harvard University，1998）。另一方面却是“被禁忌的文革”，即在中国大陆它还是一个犯禁的独立研究课题。然而，大陆的学者和作家们仍以回忆录、报 告文学等等形式书写文革。但是，无论是中国大陆的学者还是国际汉学界的文革研究都还存在不少困难。其中很重要的是原始的历史资料和系统性的整理工作的双重 缺乏。文革中的官方和非官方的出版物可谓浩如烟海，但文革后中国图书馆中的许多这类收藏都无法自由查阅。另一方面，收藏在民间和海内外学者手中的不少可贵 资源又因为缺少一个资料信息的共享工程，无法发挥它们的最大作用。由此，海内外文革研究中由史料问题引发的史实的错讹、真相的曲解和研究的空白仍普遍地存 在，阻碍着这一重大研究课题的进展。<br />
&nbsp; &nbsp; 从总结历史教训，揭示历史真相，推动文革研究这一重大历史课题的目的出发，北美地区来自中国大陆的七 位学者和一位台湾学者于1998年组成《中国文化大革命文库》光碟编辑部，开始了这一筚路蓝缕的浩大的史料编纂工程。很快，他们的工作得到了台湾、香港和 大陆学者的大力支持。香港中文大学中国研究服务中心还成了合作单位。三年多来，编者们走遍了世界各地的亚洲图书馆，征集了众多的私人收藏，抢救了无数第一 手的文字资料，在数以百万计的官方和非官方的文献中进行专业性的编辑校勘，汇集成了这一光碟中的上万篇文献。<br />
&nbsp; &nbsp;&nbsp; &nbsp; 汇编在这一光碟中的史料，限于第一手的文字资料。虽然也有一些文革后与文革密切相关的文献，但回忆录、访问记等等的二手材料均不在收录之列。就数量而言， 已经有近三千万字之巨，共分为“有关文化大革命的中共文件、指示和公报”、“毛泽东关于文化大革命的讲话、指示和文章”、“林彪关于文化大革命的讲话、指 示和文章”、“中央首长关于文化革命的讲话和指示”、“有关文化大革命的重要报刊社论文章”，“文化大革命中红卫兵、群众运动重要文献”和“文化大革命中 的异端思潮重要文献”等七大部分。为了方便中外学者查阅，文库还采用了中英文两种目录。<br />
&nbsp; &nbsp;&nbsp; &nbsp; 如此巨大的史料容量，如果采用传统的印刷出版，可能有数百本之多，连存放都很不方便。而现代计算机技术却使它们能浓缩在一盘体积极小的光碟之中，而且还能 够运用“作者”、“标题”、“时间”和“关键词”等等途径检索。从这一意义上而言，这个文库光碟正借助于现代科技，在史料汇编方面完成了一个研究性和资料 性并重的“文革博物馆”。<br />
<br />
2001年12月于美国狄金森大学<br />
<br />
<br />
<br />
序言-----余英时<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;宋永毅先生和他的朋友们费了整整四年的功夫，历经种种艰辛，编成了这一部《中国文化大革命文库》光碟。这部文库收集了一万篇以上的原始文件，总字数接近三 千万，真不愧为一项“浩大的史料编纂工程”。《文库》以类相从，分成七个单元，所收的都是第一手资料，为将来研究文革的学人提供了最方便、最重要的原始资 料汇编。参加这项计划的八位学者，包括宋永毅先生在内，都是卓然有成的专才。他们各自在专业的岗位上，利用公余的时间，遍访世界各地的亚洲图书馆，搜寻一 切有关的文件。这一困苦的过程，只有“集腋成裘”的成语才能勉强形容其万一。英国著名史学家屈维林 (G.M. Trevelyan) 有过一句名言：“去收集法国大革命的事实吧！ 你一定要下至地狱，上至天堂，去把它们找回来。”（Collect the facts of the French Revolution! You must go down to Hell and up to Heaven to fetch them.）无巧不成书，现在《文库》的八位编者竟在无意之间把这句名言实践在“中国文化大革命文库”光碟这一任务上。宋永毅先生还真的下过一次地狱。我 清楚地记得，几年前他为了收集资料，失去了几个月的自由，成为当时举世瞩目的国际事件。没有史料便根本不可能有史学，所以作为史学园地中的一个耕耘者，我 必须在这里向宋先生和他的朋友们致以最大的敬意。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;我细读了《文库》的全部目录之后，好像重温了几十年前的一场噩梦。第五部分的报 刊社论是我最熟悉的；这些文字当年是顺着发表的次序，一篇一篇读过的。那种惊心动魄的感觉现在又整个地从记忆中陡然复苏了。我不过是文革的一个海外旁观 者，情绪尚如此激荡，《文库》编者是曾经身历其境的人，他们在长期编纂过程中所感受到的精神痛苦，更可想而知。这样看来，《文库》所收的一切文件决不能和 一般所谓历史档案等量齐观，因为其中仍然跃动着强烈的生命。我可以毫不夸张地说：这里每一个文件的后面都隐藏着数不尽的血和泪。我们不难想象，当年每一个 文件发布的前后，有多少活生生的个人遭受到精神的屈辱和身体的摧残，更有多少本来很幸福的家庭顷刻之间变成“家破人亡”。研究《文库》中的文件，若不能接 触到背后那些无数淹没在血河泪海中的生命，便不免空入宝山了。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;宋永毅先生在&lt;总导言&gt;中指出了关于文革研究的一个奇 特现象：即一方面是海外有“说不尽的文革”，而另一方面在中国大陆却是“被禁忌的文革”。1978年以后，中共官方事实上已完全否定了文革，我们只要一读 《文库》第一部分有关中共文件最后十一年（1978－1988）的目录便已昭然若揭。但是在同一时期，我们又看到了许多关于文革研究的“禁忌”的规定。最 明显的是1979年3月15日“中共中央关于提醒全党维护毛主席形象的通知”，1988年12月10日“中共中央宣传部关于出版’文化大革命’图书问题的 若干规定”。中共为什么如此自相矛盾，一方面否定文革，另一方面又维护文革呢？答案并不难找：1978年后，以邓小平为首的领导班子都是被文革打倒的人， 他们如果不否定文革，在党内便没有合法性了。但是文革的根源如果步步追究下去，到达其逻辑的终点，则整个政权的合法性却又将成为问题了。“投鼠忌器”，这 是中共不得不为文革研究设下许多限制的根本原因。巴金关于建立“文革博物馆”的提议之所以得不到官方的任何回响，是丝毫不必奇怪的。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;“ 被禁忌的文革”这一事实对于我们怎样认识和研究文革具有极大的启示作用。什么启示呢？我们决不能把文革孤立起来，看作是中共“革命”进程中一个偶然的“意 外”或“偏差”。中共官方今天把文革定性为毛泽东的“晚年错误”，便是有意误导我们的思路。文革是有组织、有计划的暴力行动，而且是革命暴力的最高阶段。 但这既不是“意外”也不是“偏差”，而是革命暴力落在一个绝对独裁者掌握之中的必然结局。列宁、斯大林、希特勒、墨索里尼都曾运用有组织的暴力，有系统地 进行消灭所谓“敌人”的运动，一波接着一波。正如俄国史名家派普思(Richard Pipes) 所指出的，毛泽东的“文化大革命”不但师法列宁、斯大林，而且也参考了希特勒的经验。（见, Russia Under the Bolshevik Regime, New York, A.A. Kropf, 1993, p. 281.）列宁在建立政权后的第二年，即1918年9月，正式采用了“红色恐怖”（Red Terror）的统治方法。毛泽东一切照抄，也在1950年藉口“土改”和“镇反”，展开了大规模的屠杀。“红色恐怖”从此便笼罩着中国的大地，文革不过 是最后一个高潮而已。《文库》第六部分收有红卫兵的一篇文献，题目是“鬼见愁──红色恐怖万岁”，恰好证实了文革是“红色恐怖”的必然发展。<br />
&nbsp; &nbsp;&nbsp; &nbsp; 我们衷心地欢迎《中国文化大革命文库》光碟问世，为革命暴力蹂躏中国，保存了最宝贵的记录。但是研究文革首先必须具备历史的眼光（historical perspective）：它的近源是1949年的巨劫奇变，远源则是1917年的俄国革命。只有认识到这一点，《文库》的价值才会充分地显现出来。<br />
