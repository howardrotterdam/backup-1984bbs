													<h2>《中国文化大革命文库》光盘版</h2>
						
						
																											<div id="postmessage_648390" class="t_msgfont">来源：<a href="http://www.lsdandcrow.com/article.asp?id=2181" target="_blank">http://www.lsdandcrow.com/article.asp?id=2181</a><br />
