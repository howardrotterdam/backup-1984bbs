序言-----余英时<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;宋永毅先生和他的朋友们费了整整四年的功夫，历经种种艰辛，编成了这一部《中国文化大革命文库》光碟。这部文库收集了一万篇以上的原始文件，总字数接近三千万，真不愧为一项“浩大的史料编纂工程”。《文库》以类相从，分成七个单元，所收的都是第一手资料，为将来研究文革的学人提供了最方便、最重要的原始资料汇编。参加这项计划的八位学者，包括宋永毅先生在内，都是卓然有成的专才。他们各自在专业的岗位上，利用公余的时间，遍访世界各地的亚洲图书馆，搜寻一切有关的文件。这一困苦的过程，只有“集腋成裘”的成语才能勉强形容其万一。英国著名史学家屈维林 (G.M. Trevelyan) 有过一句名言：“去收集法国大革命的事实吧！ 你一定要下至地狱，上至天堂，去把它们找回来。”（Collect the facts of the French Revolution! You must go down to Hell and up to Heaven to fetch them.）无巧不成书，现在《文库》的八位编者竟在无意之间把这句名言实践在“中国文化大革命文库”光碟这一任务上。宋永毅先生还真的下过一次地狱。我清楚地记得，几年前他为了收集资料，失去了几个月的自由，成为当时举世瞩目的国际事件。没有史料便根本不可能有史学，所以作为史学园地中的一个耕耘者，我必须在这里向宋先生和他的朋友们致以最大的敬意。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;我细读了《文库》的全部目录之后，好像重温了几十年前的一场噩梦。第五部分的报刊社论是我最熟悉的；这些文字当年是顺着发表的次序，一篇一篇读过的。那种惊心动魄的感觉现在又整个地从记忆中陡然复苏了。我不过是文革的一个海外旁观者，情绪尚如此激荡，《文库》编者是曾经身历其境的人，他们在长期编纂过程中所感受到的精神痛苦，更可想而知。这样看来，《文库》所收的一切文件决不能和一般所谓历史档案等量齐观，因为其中仍然跃动着强烈的生命。我可以毫不夸张地说：这里每一个文件的后面都隐藏着数不尽的血和泪。我们不难想象，当年每一个文件发布的前后，有多少活生生的个人遭受到精神的屈辱和身体的摧残，更有多少本来很幸福的家庭顷刻之间变成“家破人亡”。研究《文库》中的文件，若不能接触到背后那些无数淹没在血河泪海中的生命，便不免空入宝山了。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;宋永毅先生在&lt;总导言&gt;中指出了关于文革研究的一个奇特现象：即一方面是海外有“说不尽的文革”，而另一方面在中国大陆却是“被禁忌的文革”。1978年以后，中共官方事实上已完全否定了文革，我们只要一读《文库》第一部分有关中共文件最后十一年（1978－1988）的目录便已昭然若揭。但是在同一时期，我们又看到了许多关于文革研究的“禁忌”的规定。最明显的是1979年3月15日“中共中央关于提醒全党维护毛主席形象的通知”，1988年12月10日“中共中央宣传部关于出版’文化大革命’图书问题的若干规定”。中共为什么如此自相矛盾，一方面否定文革，另一方面又维护文革呢？答案并不难找：1978年后，以邓小平为首的领导班子都是被文革打倒的人，他们如果不否定文革，在党内便没有合法性了。但是文革的根源如果步步追究下去，到达其逻辑的终点，则整个政权的合法性却又将成为问题了。“投鼠忌器”，这是中共不得不为文革研究设下许多限制的根本原因。巴金关于建立“文革博物馆”的提议之所以得不到官方的任何回响，是丝毫不必奇怪的。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;“被禁忌的文革”这一事实对于我们怎样认识和研究文革具有极大的启示作用。什么启示呢？我们决不能把文革孤立起来，看作是中共“革命”进程中一个偶然的“意外”或“偏差”。中共官方今天把文革定性为毛泽东的“晚年错误”，便是有意误导我们的思路。文革是有组织、有计划的暴力行动，而且是革命暴力的最高阶段。但这既不是“意外”也不是“偏差”，而是革命暴力落在一个绝对独裁者掌握之中的必然结局。列宁、斯大林、希特勒、墨索里尼都曾运用有组织的暴力，有系统地进行消灭所谓“敌人”的运动，一波接着一波。正如俄国史名家派普思(Richard Pipes) 所指出的，毛泽东的“文化大革命”不但师法列宁、斯大林，而且也参考了希特勒的经验。（见, Russia Under the Bolshevik Regime, New York, A.A. Kropf, 1993, p. 281.）列宁在建立政权后的第二年，即1918年9月，正式采用了“红色恐怖”（Red Terror）的统治方法。毛泽东一切照抄，也在1950年藉口“土改”和“镇反”，展开了大规模的屠杀。“红色恐怖”从此便笼罩着中国的大地，文革不过是最后一个高潮而已。《文库》第六部分收有红卫兵的一篇文献，题目是“鬼见愁──红色恐怖万岁”，恰好证实了文革是“红色恐怖”的必然发展。<br />
&nbsp; &nbsp;&nbsp; &nbsp; 我们衷心地欢迎《中国文化大革命文库》光碟问世，为革命暴力蹂躏中国，保存了最宝贵的记录。但是研究文革首先必须具备历史的眼光（historical perspective）：它的近源是1949年的巨劫奇变，远源则是1917年的俄国革命。只有认识到这一点，《文库》的价值才会充分地显现出来。<br />
