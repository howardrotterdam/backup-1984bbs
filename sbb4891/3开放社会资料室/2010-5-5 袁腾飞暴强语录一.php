袁腾飞：1972年美国总统尼克松访华，刚下飞机，映入眼帘的是一行大字“打倒美帝国主义”，忘了拆了，来不及了。尼克松在“打倒美帝国主义”几个大字前面检阅三军仪仗队，幸亏他不认识汉字，他要问，翻译肯定跟他说“welcome&nbsp;&nbsp;to&nbsp;&nbsp;china”。<br />
<br />
袁腾飞：咱们现在最常用的一个词“海峡两岸”是美国人发明的。<br />
<br />
袁腾飞：日本当年跟中国建交，咱们付出了相当大的代价：我们放弃了对日的战争赔偿，当时如果索取这个赔偿的话，大概是1800亿美元！毛泽东和周恩来们认为如果我们索要这笔赔偿，这笔钱就会转嫁到日本人民身上，战争是日本军阀发动的，日本人民是很无辜的。咱们中国老是这么认为：人民跟政府是对立的。除了中国，哪的人民跟政府都是一条心，来中国侵略的日本兵是日本贵族子弟吗？没有日本农民、矿工吗？哪一个不是日本人民？但咱们就认为不该要这笔钱。<br />
<br />
袁腾飞：中国的外交关系出现重大转折，跟西方国家的关系改善，恰恰是在文革时期。那是因为两方有共同的敌人---苏联，等这个共同敌人一没，中美两国也就掰了，回不到七八十年代的蜜月水平了，掰也掰不到朝鲜战争的水平。<br />
<br />
袁腾飞：中美关系三大瓶颈或障碍：贸易问题、人权问题、台湾问题。<br />
<br />
袁腾飞：你看胡总、江总、温总啊到美国、欧洲访问，都得带着什么去啊？都得带着钱去，带着支票本，跟那一花几十亿欧元、美元，人家才有空接见你，顾客是上帝嘛。<br />
<br />
袁腾飞：中美关系三大障碍之一：人权问题，最简单的，死刑问题。取消死刑是板上钉钉的事，肯定会取消死刑。死刑在西方国家看来，属于“同态报复”。最早的时候，你把我眼睛挖了，法官就判决我把你眼睛挖了，死刑亦属于“同态报复”法。世界上2/3的国家是废除死刑的。美国日本只有一种情况判死刑：就是一级谋杀，即故意杀人。韩国7种罪，印度是4种罪，中国是80种罪适用于死刑。<br />
<br />
袁腾飞：我认为除了故意杀伤人命，其他的不应该判死刑，你比如说贪污，贪污真不应该判死刑，你崩了他到痛快了他了。按毛泽东的话讲：“让他为人民生产小米嘛”，你原来日进斗金，一天一千万，现在让你天天掏粪，他最后一脑袋扎进粪坑里了，用不着你判他死刑。<br />
<br />
袁腾飞：我认为除了故意杀伤人命，其他的不应该判死刑，你还得看什么样的故意杀伤人命：你比如说咱俩搞对象，你背叛我，我把你掐死，这种人也不应该判死刑，因为谁跟他搞对象他掐死谁，对吧,你别跟他搞对象不就完了嘛，他不会掐不相干的人。那种拦路抢劫，这种人你可以崩了他，所以判死刑的刑种应该减少。<br />
<br />
袁腾飞：外国现在攻击中国你为什么坚持那么多判死刑啊？就因为那死刑犯的器官你可以卖，可以移植，当然死刑犯都是签我“自愿”捐献遗体，罪犯也有的有一种悔罪心理，反正我也要死了，把握剌吧剌吧谁需要给谁吧，但是谁需要给谁，你给人家的时候是需要要钱的。<br />
<br />
袁腾飞：中国当年猛挖地道、防空洞用来防原子弹，就那玩意儿，手榴弹都防不了。<br />
<br />
袁腾飞：1976年中国人民解放军达到了天文数字：661万人，比朝鲜战争时还多60万，当时全国上下最流行的颜色就是绿军装。国民财富全都浪费在扩军备战当中，就跟现在的北朝鲜一样嘛，先军政治。<br />
<br />
袁腾飞：今天世界上谁有能力发动世界大战？这几个有核国家！只要这几个国家不落在丧心病狂的独裁者手中，爆发世界大战的可能性是不存在的。<br />
<br />
袁腾飞：中国新闻联播头25分钟伟大光荣正确，后5分钟世界各国灾难。<br />
<br />
袁腾飞：美国苏联的核武器如果全爆炸的话，地球半年见不着太阳，气温降到零下六十度，所有的生命全部灭绝。核战争没有最后的胜利者，正是因为有了这种恐怖的武器，是人类享受了60年的和平。<br />
<br />
袁腾飞：法国的军队搞了一个调查，说如果现在爆发世界大战，开展5分钟，法军70%精神崩溃。尤其像中国都是独生子女，像你们这样的送到战场，炮火硝烟，旁边咣一声你哥们脑袋送上天去啦，胳膊腿落你身上，你立刻就疯了。<br />
<br />
袁腾飞：那年中非论坛，非洲欠咱100亿美元，咱手一挥，不要了，你要他也没有，完事咱又给他30亿。老黑就这样，大陆给钱就跟大陆建交，台湾给钱就跟台湾建交，这帮人寡廉鲜耻，背信弃义，翻脸比翻书都快，心比脸黑多了那帮人。为什么使劲给他们砸钱，这帮老黑联合国一国一票把中国抬进联合国。<br />
<br />
袁腾飞：梵蒂冈跟台湾建交，跟中国大陆不建交是因为条件谈不拢：梵蒂冈认为应该罗马教宗任命主教，咱们坚决不同意，咱们认为主教应该国务院宗教事物管理局任命。实际上，西方谚语说得好：“上帝的归上帝，凯撒的归凯撒”，宗教是宗教，政治是政治。</div>

							
							
							
							
