<br />
<br />
好莱坞的电影，很多影评是不屑的，商业片，常见于评论中。可我仍然很爱看，是因为想不出商业片相对应的是什么影片。最近，又看了一本，片名叫调包婴儿，是一个真实的故事。仍然觉得非常好看，以至于看到凌晨3点。<br />
<br />
　　 　　<br />
<br />
　　影片的背景是1928年的3月的洛杉矶。满街跑着福特T型车般的汽车，和漆成红色的火车车厢般的有轨电车，当当作响。女人们穿着过膝的呢大衣，戴着缀着花的窄沿圆帽，帽下的头发长而卷曲。男孩子们一副绅士似的，鸭舌帽，打着领结的咖啡色西装，裹到小腿的长袜。精美的画面，散发着上世纪二三十年代浓郁的咖啡般的怀旧气息。尽管不是我们的本土文化，而我也究竟不知道那个时期洛杉矶的模样，透过画面，完美的布景，看得出导演颇费心机。我常想，导演哪整这么多的老爷车。不过这并不是本文所关注的。<br />
<br />
　　 　　<br />
<br />
　　安吉丽娜.朱丽饰演一位失踪男孩的母亲。一个加班后的下午，回到家中，大约7.8岁的儿子失踪了。故事就此展开。母亲报案后，消息也通过牧师传遍整个区域。很快，精明的警察队长通知这个伤心欲绝的母亲，孩子找到了。故事发展到这里，似乎很平淡。<br />
<br />
　　 　　<br />
<br />
　　火车站母子相遇那一刻，母亲几乎惊呆，分明不是他的孩子。可那男孩一口咬定，警察队长肯定的使人怀疑那母亲的精神状况。母亲无可奈何的接受这一事实后，被隔离的媒体蜂拥而至，不容母亲解释，警察局长已经确认这是胜利的团聚，而警局也时时刻刻全心全意为人民服务。这话听着耳熟。误导的媒体充斥着被误导的言论，孩子找到了。指鹿为马的事不是中国古代特有的。<br />
<br />
　　 　　<br />
<br />
　　领回家的男孩出奇的顽固，任凭那母亲怎样询问，始终咬定自己就是她的孩子。甚至面对学校陌生的老师也镇定自若。难以想象这个7、8岁的脑袋里面究竟装着的是什么。动荡的非洲，持着AK的娃娃兵，冲锋陷阵比真正的士兵还要勇敢，他们是怎么了。孩子是单纯的，是容易被洗脑的。最后，这个男孩被真正的母亲领走时，面对记者道出真相，是警察让他干的。看样子，全心全意为人民服务，是谁都讲得出来的。<br />
<br />
　　 　　<br />
<br />
　　影片中的牧师，是正义的化身。其实是言论自由的一个符号。自己办着一个小小的广播电台，通过电波揭露警界的黑暗、私利和贪欲，人民的保护者却成了施虐者。1928年的美国是没有新闻审查制度的，才使警察局长在办公室收听痛骂他的声音成为可能，1928年的美国是没有因言获罪的，才使民间的力量揭露黑暗的政治成为一种可能。牧师也曾经警告过那母亲，她将陷入困境。那母亲是没有政治觉悟的，只想找回自己的孩子，远离政治。独立宣言有一段：过去的一切经验也都说明，任何苦难，只要是尚能忍受，人类都宁愿容忍，而无意为了本身的权益便废除他们久已习惯了的政府。那母亲确是如此。其实，政治是谁也躲不掉的。<br />
<br />
　　 　　<br />
<br />
　　那母亲将诸多证据，甚至牙医的报告公布媒体后，警察恼怒了。当那母亲在警察队长面前仍然坚持男孩不是自己的儿子时。被非法的剥夺话语权，投进了疯人院。疯人院，一个不经法定程序，非法剥夺公民权利的象征。谎言重复千遍就是真理，警察就是真理，权利就是真理。记得有人撰文，言论自由不如一块面包。他错了，被剥夺了言论自由，餐桌前的面包都会被别人抢走，坐牢的却是自己，这是影片的启示之一。<br />
<br />
　　 　　<br />
<br />
　　疯人院的护士，一个个面无表情，出手狠毒，好像机器人一般，完全失去人类的良知。一幅爪牙、打手的形象。疯人院关押的大都是勇于揭露政府、警界黑暗的。暴力袭警的杨案，记忆犹新，案件本身已经盖棺定论，杨十恶不赦已赴黄泉。但他尚不能获得充分的辩护，律师竟然是指定的，而可以给他聘请律师的母亲却在其执行死刑后在北京一家精神病院被找到，真是滑稽。当然，我并没有为他辩护的任何想法。这样的事是不是仅此而已，不好说。疯人院不是权利的后花园。<br />
<br />
　　 　　<br />
<br />
　　案件终于水落石出，一个下级警察，是负责的。尽管破案的线索放在面前，仍不能说服警察队长。政府内部的纠错仍然是困难的，下级屈服于上级的绝对权利。一铲，一铲的黄土剥去，逐渐显露被害孩子的骸骨，真是让人心碎！孩子都是天使。<br />
<br />
　　 　　<br />
<br />
　　疯人院的医生尚是有一点良知未泯的知识分子，获知真相后，放了那位母亲。母亲加入了揭露黑暗的行列。牧师、律师、那母亲和民众冲进疯人院解救非法关押的民众一幕大快人心。那个冷面的护士尚要阻拦，却在法律和正义面前吓的惊恐万状。民主是法制的基石，民主使法治成为可能，法制才使扫清权利的死角成为现实。指责民主的大有人在，指责法制的却没有，隔断民主与法制的关系，是指责民主的一个小小手段。还好，美国是民主的，是法制的，以至于牧师、律师和民众就能洗清那么多无端被关押人的冤屈。期待明君治世，只是一个美好而天真的想法，杨乃武、小白菜式的洗冤机遇太低、太具有戏剧性，现实是残酷的。<br />
<br />
　　 　　<br />
<br />
　　终于，变态杀人狂被执行绞刑。听证会后，警察队长被停职，局长被降级，市长再未参与连任竞选。一任政府就此倒台。<br />
<br />
　　 　　<br />
<br />
　　我到更愿意将失踪的孩子解读为公民的权利，或者说人权，而黑暗的而警察解读为政府的代理。美国的民主，美国的选举制度，民众的言论自由和不因言获罪，美国健全的法制，终于使自下而上的揭露和纠正成为现实。“当任何形式的政府对这些目标具破坏作用时，人民便有权力改变或废除它，以建立一个新的政府。”尽管美国民众可以合法持有枪支，但健全的制度使得只需言论就能实现。一切置于法律之下，影片中的法官就是法制社会的代言。<br />
<br />
　　 　　<br />
<br />
　　影片中变态杀人狂反复说没杀那母亲的孩子，警察局长与队长的对话：不论他在不在那牧场，事实就是可能在某个地方死了。这两端情节，一直交织在我的耳边。是变态杀人狂杀了那孩子，还是警察为了掩盖错误杀了那孩子。我都不敢肯定，影片也没给出答案。官员都是混蛋，没有监督，没有选举的官员更是混蛋。<br />
<br />
　　 　　<br />
<br />
　　影片似乎还给了一些启示。政府更替，管理出现真空，我是不愿用权利两个字替代管理的。或者法律也出现真空，是信仰在维持社会的秩序和重建的。信仰是必须的。说到这，可能有人要反对了。<br />
<br />
　　 　　<br />
<br />
　　这样的片子，我想，大约是国内那些只会反副职的腐败的导演拍不出来的吧。<br />
<br />
　　 　　<br />
<br />
　　后记，真是一部好片，值得一看。不论是左派还是右派都值得一看。左派可能和我有不同的解读，会说，瞧，民主社会也是这样混蛋的官员，黑暗的政治。右派的看法我就不重复了。中间派也应该一看，就当一本商业片。</div>

							
							
							
							
