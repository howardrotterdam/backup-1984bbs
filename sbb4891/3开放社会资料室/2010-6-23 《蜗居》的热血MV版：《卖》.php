尽论中国：《蜗居》的热血MV版《卖》<br />
<br />
内地电视连续剧《蜗居》，因在楼市飙升之际触及房奴问题而轰动一时，但被国家广电总局官员批评为「靠荤段子、官场、性等话题来炒作」，而在内地遭禁播。当局频频出招打压之後，近期内地楼价有所回落，但对於打工仔来说，买楼仍是遥不可及的梦想。被称为《蜗居》MV版的《卖》，又因为有「宁愿流血不流泪」之激愤之词，传闻难逃遭禁命运。<br />
<br />
由内地歌星黄征演唱的《卖》（<a href="http://www.duomoo.com/?p=4608" target="_blank">http://www.duomoo.com/?p=4608</a>），一在网络上曝光就大受欢迎。《卖》以楼价为主题，画面中不断出现不同层次、不同收入的人，为了买座「锦绣花园」需要工作的年份，包括文员要87年、的士司机要125年、清洁工要255年、地盘工要514年，他们为此流泪、流血、吐白沫，而老板只要5天，老板身边的女伴更只要「一拱」──用胸部去拱老板。<br />
<br />
为生存干甚麽都是『卖』<br />
<br />
黄征曾表示，《卖》要唱出都市人为生活为人生撕心裂肺的呼喊，「就是为了生存，无论你在干甚麽，都是某种意义上在『卖』。建筑工人卖的是力气，创意家卖的是创意，商人卖的是商品，还有的不惜出卖朋友的感情来换取金钱，甚至有些人在出卖身体，这些都是我们身边活生生的例子。」<br />
<br />
震撼数字唱出民怨沸腾<br />
<br />
画面上震撼人心的数字，就是房奴的控诉，而歌词亦相当犀利、尖刻，唱出民怨沸腾的现实：「我宁愿流血不流泪，我宁愿看自己受罪，出卖自己的疯狂，何必伪装；我宁愿流血不流泪，我宁愿去笑着受罪，出卖自己的信仰，多麽荒唐。」<br />
<br />
其实，楼价只是社会问题的一个侧面而已，房子问题也只是《蜗居》、《卖》用以串起故事的轴线，《蜗居》吸引观众的还有官场权斗、官商勾结、二奶等问题，《卖》震撼人心的还在於都市人理想幻灭後的挣扎：「想活出个人样，却被掏空了心脏，感觉梦太远了，有人卖掉了翅膀。」<br />
