													<h2>批判禁片《红色角落》</h2>
						
						
																											<div id="postmessage_66077" class="t_msgfont">（转）电影《红色角落》曾是一部备受瞩目和争议的政治惊悚片，在我们国内属于禁片。<br />
<br />
该影片讲述了一个美国律师杰克(李察・基尔饰)代表一家大规模的西方文娱乐集团来到中国首都--北京，准备完成中共批准的第一笔卫星通讯交易。当他开完会后邂逅了一名中国美女(孟广美饰),并在当晚与其共渡春宵，隔日一早却发先她陈尸身旁。杰克当时即被公安逮捕。 杰克在公安局审问过程中受到了所谓&quot;中国特色&quot;的虐待，因为受害者的父亲是中国解放军部队的一位将军，这个案子倍受高层关注。杰克代表的公司不愿意与中共为敌，没有采取多少营救措施，打算让他在中国司法制度中自生自灭。在法院对此案件的受理过程中，明显的带有快速给杰克治罪的倾向，杰克只有把唯一希望寄托在法院指派的公设辩护人沈玉玲(白灵饰)身上。起初沈玉玲本来要杰克认罪，但杰克坚持他无罪、沈律师为他大无畏的精神所感动，加上证人的证词及证据漏洞百出，在沈玉玲抽丝拨兰的调查下，一步步发现案情真相，最后终于揭发一名中共高干为了包揽这项卫星通讯业务代理而谋化的不法行为，为杰克洗清了冤屈。<br />
<br />
这部影片之所以在大陆遭禁，是因为该片内容带有反共反华的歧视情绪，很多情节纯属无中生有的捏造。其中有中共当局限制民众言论和接受外部传媒自由，公安人员暴力执法甚至虐囚（其实美国大兵在伊拉克也虐囚），以及表现政府官要对法院判决结果的强大干涉力量力量等情节。而这些情节和我们现行的影片发行制度有悖，所以国内禁播。<br />
<br />
我看完这部片子后觉得，这部片子带有明显的美国式的逻辑思维和审美方式，以这种方式表现和审判我们中国的文化和生活，让我们中国人觉得很是荒唐和不贴近生活。<br />
<br />
说说其中几个印象比较深的情节:<br />
<br />
1.杰克作为嫌疑犯受审和被关押的情节。<br />
<br />
以前从电视电影中看到公安审问犯人的过程大致是:犯人和几个公安对坐在一个比较封闭且光线昏暗的屋子里，屋子的墙上贴有“坦白从宽、抗拒从严”的醒目标语，公安往往表情肃穆、声色犀利，喜欢用一个高功率的台灯刺激犯人的眼睛；囚犯要么吓的浑身发抖要么就掉而郎当...<br />
<br />
杰克的受审过程差不多，但是就是没有国内影片那种双方针锋相对的言语和心理交锋，公安人员直接就告诉杰克:我们搜集了证据，你杀人了，不要想狡辩，并反复强调:认罪吧，在这里认罪态度好能好受些。<br />
<br />
还有，杰克被关押的情节有些伊拉克犯人被美国大兵虐囚的那种感觉，让人非常震惊:冷漠蛮横的监狱看守、连张床都没有的阴暗牢房、锈迹斑斑的铁门、狗食般的饭菜...中国有句老话叫“善有善报、恶有恶报，不是不报，时间未到”，我想这句话多少反映了国人遇见坏人侵犯时更愿意忍声吞气和息事宁人，同时往往通过这种无奈的诅咒来宣泄自己的气愤。但是当影片营造出身临其境的监狱生活时,每个人的心灵深处都会受到强大的震撼,尊重和维护作为人的基本权利的思想被唤醒。当然，这部1997年发行的美国大片在丑化我国人权制度的时候可能万万没有想到，如果今天发行这部影射他国政治的影片会是让全世界人民反嘲这个尊重人权的国家在伊战中犯下滔天罪行吧。<br />
<br />
2.杰克在法庭受审的情节。<br />
<br />
在杰克在法庭受审的情节中，观众很明显的感觉到那个庄严肃穆的法庭被一种不可见的强大力量左右着。这个力量不是正义、不是人权、不是民主、不是道德。影片情节最后交代，真正罪犯的父亲是和中央领导交往甚密的一位高干领导。此情节带有明显的反共色彩，影射我国人权体制:法律面前并不是人人平等，特权阶层凌驾与法律之上。但是我却在网上看到了很多网友留言说国内现实中确实存在不少这类事情，虽然我不可考证，但是我想，我们法庭要是能够在“公平、公正、公开”上多下功夫，以正视听，让老百姓身有体会，这些国外电影莫须有的攻击情节就是让老百姓看，其不真实性也会不攻自破的。<br />
<br />
3.杰克和中国律师沈玉玲的感情发展情节。 <br />
<br />
这位叫沈玉玲的女律师最后帮助杰克打赢了这场官司，体会了在中国“神秘”的司法体制下得到这样一个结果的艰辛，并被杰克的坚强、勇于牺牲和绅士风度深深的吸引而爱上了他。<br />
<br />
这些感情情节我认为是狗屁不同，全是美国佬瞎编的，非常的垃圾！看的出导演并不将这些感情戏作为重点，泛泛带过，反共反华才是他的主要目的！<br />
<br />
美国人拍电影真是的，为什么要影射政治呢！？<br />
<br />
提一下这部电影里的一句台词。其中由法院操纵的法官角色本想早早给杰克定罪，结了这个案子，一方面给受害者的父亲（将军）一个交代，另一方面也让幕后真正的凶手能早日安心。但杰克和沈律师却丝毫不放弃洗清冤屈的机会，他们寻求大使馆保护、搜罗证据...几经波折，到了影片末尾，当沈律师在法庭上为杰克据理力辩，在证据越来越证明杰克无罪的时候休庭了，沈律师被叫到法院院长办公室，院长和一堆领导给她做思想工作，沈律师最后一字一句的对他们说:“我们在沉默中生活的太久，从一个法庭逃到另外一个法庭，为什么我们不能在光天化日之下评判？为什么我们不能够相信人民群众来做见证？为什么我们不能够允许他们说话而不把他们压碎在沉默中？...”<br />
<br />
这句话我想不仅仅是针对我们的司法体制，当年鲁迅先生四处《呐喊》:“不在沉默中爆发就在沉默中死亡.”，为的就是唤起民众争取民主、争取自由、争取人权的思想觉悟，今天胡主席告戒我们年轻人要树立新时期的社会主义荣辱观。谈到了“八个光荣，八个可耻”，有一条是“以崇尚科学为荣、以愚昧无知为耻”，愚昧无知既然可耻，那么我们就要争取现实社会的知情权，好的现象支持学习、坏的现象批判反对，这才是良好的社会氛围</div>

							
							
							
							
