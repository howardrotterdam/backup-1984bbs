													<h2>看方绍伟的“中国政治为什么还没有崩溃”还不如看这个文章更有说服力</h2>
						
						
																											<div id="postmessage_499135" class="t_msgfont">冼岩：为什么西方民主不适于今日中国<br />
中国未来前景如何，稳定还是动荡，繁荣还是衰退？回答莫衷一是。但是，学术界的主流意见已逐渐倾向于：在相当长时间内，中国仍能保持稳定。但稳定为什么能够持续？在什么情况下，稳定将被打破？没有令人信服的论证。相关研究和讨论多停留表面，没有找出中国社会治乱翻转的根本原因。<br />
<br />
为什么西方民主宪政在中国特别艰巨难行？为什么会有如此多反复？为什么在国际国内的强大压力下，执政党仍然坚持一党专政，不愿意启动政治改革？为什么国际国内、民间学界的多方呼吁、多种预测都只是一厢情愿，无济于事？这些反复发生的事实都不是偶然，而是有一种客观必然性深藏其中，因此它不以人的主观意志为转移。<br />
<br />
本文试图解答这些问题，揭示中国政权赖以存续的深层逻辑。<br />
<br />
一，集权体制的合法性<br />
<br />
“百代皆行秦政制”，无疑，中国于1949年建立的政权，虽然披上了历史决定论的意识形态外衣，但在政治权力的基本建构上，仍然是沿袭千年古制。用毛泽东的话说，是“马克思加秦始皇”。<br />
<br />
这种政治权力结构有两大特征：一是中央（最终要具体到最高统治者个人）的绝对权力，这一权力既不受任何限制，也不存在任何制衡；二是政治权力对社会的全面控制，其目的是为了禁绝一切可能出现的对政治权力的挑战，或者是禁绝新的不受权力控制的社会力量。<br />
<br />
政权的合法性，是现代政治学的重要命题，意指公民对政权的理性认可和程序授权。但是在集权社会，政治有其自身逻辑，合法性也有其自身含义。它包含两个方面，即政权本身的合法性和与其相联的政治制度的合法性。集权政治的合法性是一种逆向选择意义上的合法性，合法的统治者不是国民自由选择的结果，而是他们别无选择的结果。合法性表现为程序和操作上的别无选择性：除此之外，再无可选。民众除了决心以暴易暴，不惜对抗强大的国家机器外，只有接受这种唯一的现实可能性，默认唯一统治者的现实合法性。<br />
<br />
可见，集权政治的合法性在于其别无选择性；一旦出现新的可能选择，统治者的合法性就摇摇欲坠，这是集权政治的最大隐秘。正是为了保证别无选择的有效持续，统治者才必须建立自己不受制衡的绝对权力，必须对社会实行绝对控制，不容许任何可与自己讨价还价或分庭抗礼的力量、因素和苗头存在。<br />
<br />
当然，集权政治在意识形态上也有一套说词来论证自己的合法性。但这种意识形态的自我论证同样是建立在别无选择的程序性之上的，它不是权力的来源和根本，而只是帮助统治的手段（即“治术”）。正如民主政体合法性的实质在于民意授权的程序正义，集权政体的合法性根基则是其程序上、操作上的别无选择性。<br />
<br />
由此我们可以明白，为什么一些体制外的“合理化”建议，例如要求政府开放言论，让民众有合适的自我表达渠道，与政府展开对话，以化解矛盾，释放不满等等，看起来似乎有百利而无一害的主张，却总不能被政府采纳。<br />
<br />
在民主制度下，沟通和对话，确实能释放负面能量，缓解社会矛盾；讨价还价，确实能平衡各方利益，化解冲突，达成妥协，这是民主制度内部社会自我调节的有效方式。但是在集权制度下，由于保持政权的别无选择性所必须的持续高压，放开社会协商和讨价还价的渠道必然释放被压抑的矛盾和不满，刺激更多的欲望和冲动；此时，被压抑的利益和力量要求自我伸张、自我实现，对社会的绝对控制将不再可能，政权的别无选择性也将失去保障，并时刻面临威胁。正是这种中西方社会不同的政治逻辑，决定了双方对待言论自由和公民社会的不同态度。不了解这一点，就不能了解政府行为的深层逻辑，就会对现实抱有不切实际的幻想。 <br />
<br />
由此我们也可以明白：所谓“共产党是当今中国唯一有领导能力的政治组织”，确实是中国政权的合法性证明。但是这种“唯一”合法性，正是执政者本身所作所为造成的，是传统政治逻辑演绎的必然结果。更重要的是，这种唯一合法性并非只有主观价值意义，它更是一种客观的现实存在。社会批评者尽可以在道义上蔑视它，批判它；但它依然会岿然不动，并散发出巨大能量，使它的反对者在现实中头破血流。<br />
<br />
集权统治者的合法性来源于民众的别无选择性，别无选择性又形成于绝对权力和绝对控制。因此，只要执政者不打算自动放弃权力，寄希望于它自己放松社会控制、形成权力制衡，就是不现实的。如果政权本身没有建立起新的合法性，对别无选择性就依然有着绝对的依赖，那么，任何在政权之外松动控制、培植制衡的努力，必然遭到政权力量的极力压制而变得前途渺茫。不错，关于赋税的讨价还价确催生西方的代议制民主，所谓“无代议士不纳税”。但这一因果成立的前提是：在此之前，纳税人已拥有为王权所不能剥夺的独立地位和独立利益，已拥有和统治者讨价还价的能力。但在中国，在绝对权力的绝对控制下，纳税人何曾有独立地位可言？又何来与政府讨价还价的凭籍？在中国，可以说，任何团体或个人，都没有与政府正面博弈的能力。政府讲什么就是什么，要收多少税就收多少，纳税人除了逃避、弄虚作假，别无利益伸张的办法。在这样一种权力结构下，政权以外的政治力量，既找不到立足处，更找不到着力点，因此，几乎不可能自我孕育出对权力的制衡。这就是今日民间社会似乎逐渐活跃，但始终不能对政治权力构成有效制约的原因。一些人关于地方分权及联邦制的设想、憧憬，也与此类似，同样只是基于西方经验设计的政改路向。由于中西方巨大的背景差异，相同取向会按照不同逻辑，演绎出不同结局。<br />
<br />
规律之所以成为规律，是因为其客观必然性，是因为它不以人的主观意志为转移。因此，尽管几十年来，多方势力为政治改革奔走呼吁，政改却一直停滞不前。无可否认，1980年代初期，执政党高层许多人也热衷于政治改革，认为这是复兴中国所必须迈过的门槛。但随着形势发展，隐藏在事实背后的规律逐渐展开，政治改革与传统政治权力结构的不相容性日益凸显。最后，党内改革派别无出路――要么放弃政治改革，要么放弃自己的政治权力。<br />
<br />
甚至邓小平本人，在1980年代初也曾提出过一些关于政治改革的设想。但是，作为现实主义的政治家，邓很快意识到政治改革与保持政权的二律背反，于是树起“四项基本原则”的藩篱，为改革划定了单行道。1980年代末的事件，从某种意义上证实了邓的判断。从此，搁置政改成为党内高层共识。迥异于苏东原社会主义国家的社会转型，中国改革呈现出经济跛足独行的“中国特色”。外界至今仍对某些党内改革派、或者是言论开放人物持有某种不切实际的幻想。实际上，这些人的言论开放，往往与其政治上被排挤、处于旁观位置是联系在一起的。一旦大权在握，站到前台，他们同样不可能逃脱客观规律的制约，不可能跳出客观局限性。<br />
<br />
二，合法性危机与双重合法性<br />
<br />
既然别无选择性是集权政治的基础，那么只有当这一合法性被打破即出现新的现实选择之时，绝对权力和绝对控制才可能松动。中国历史上历代王朝的变迁都默守这一程序。在历史上，政权更迭主要有两种模式：一是民不聊生时，别无选择的选择也成了不能忍受的命运，民众情愿以身犯险，对抗强大的国家机器――“民不畏死，奈何以死惧之？”此时，别无选择不再构成程序性限制，政权的合法性丧失，民众揭竿而起，起义势力带给人们新的选择。其时民众不再是别无选择，他们可以多中择一。民众的选择，所谓民心向背，往往决定着政权争夺的最后结果。一方获胜后，又会形成新的别无选择的政治权力，历史就如此往复循环。<br />
<br />
另一种模式是外来力量击败本国政权，其势力侵入本国范围，代替前面的起义力量为民众提供新的选择。<br />
<br />
无论哪一种模式，社会控制的铁幕只要被掀开一角，反对原政权的力量只要在某一处站稳脚根，各种被压抑、被控制的力量就可能在此处汇聚起来，形成联动。所谓风云际会，多米诺的骨牌就可能因此坍塌。所以，集权政治既强大又脆弱。强大，是因为它全无破绽；脆弱，是因为它容不得一点破绽，任何破绽都可能致命。<br />
<br />
具体来看清末，其时统治者权威和合法性的逐渐丧失，与历史上有所不同。一是外来力量虽然强行进入中国，但它们只热衷于瓜分经济利益和势力范围，并无意攫取政权；二是外来力量不但打败了清政府，代表了一种新的势力；同时，它还带来一种新的价值理念和政治制度，代表一种不是政治权力上，而是政治制度上的新选择。列强武力上的胜利，社会经济福利的发达，都证明了这种政治制度的优越。因此，传统的政治制度丧失了合法性的自我说明，丧失了令人信服的权威。于是，统治者不得不着手重建政治制度的合法性，冀图重拾权威，这样，才有了洋务运动、戊戌变法和清末立宪。<br />
<br />
在全球化背景下，别无选择的政治模式之保持极端困难。信息的开放，经济文化的交流，在在昭示了政治制度可以有多种选择。当今中国政权面临的是类似晚清政府的政治制度合法性危机。如果说，共产党作为执政党是现实条件下别无选择的唯一选择，那么政治制度呢？我们为什么不能拥有新的、更好的制度？无论是西方发达国家的现实成就，还是苏东社会主义国家的转型示范，都时时在提供参照，证明了政治制度方面其他选择的可能性。至此，政权开始面临最棘手难题：要么证明自己（政治制度）的合法性，要么改变自我（政治制度）。<br />
<br />
经过了清末、苏东及自身1980年代的教训后，执政党不再一味将赌注压在自我改造（即政改）上，而是力图先自我证明，然后徐图改变。值得庆幸的是，现行政治制度的合法性证明至今并不难找到，这就是“政绩合法性”。中国政府能够领导中国取得持续30年的经济增长以及社会多方面的改进，比诸苏东转型的痛苦，可以说证明了现行政治制度在现阶段的合法性，证明了中国现在所走改革路径是合乎实际的。<br />
<br />
按照“实践是检验真理的唯一标准”这一命题，此一合法性证明至今仍然成立。照搬西方模式曾经造成民国初年的混乱，苏东在今天的西化历程也充满痛苦，中国道路相对于苏东转型的顺利性（这种对比应加上中苏基本条件不同的参数），一举证明了中国现行模式相对于苏东模式和照搬西方的优越性，至少证明它更适合于中国国情。但是，这一证明只是到此为止。接下来要命的问题是：对于建立在“政绩合法性”上的现行政治制度来说，今后将面临“有政绩则有合法性，无政绩则无合法性”的尴尬――政治制度的合法性源泉，反过来成了制度本身的致命软肋。当政绩下滑造成政治制度合法性危机时，执政党要解决的难题是：怎么从政治制度中脱身而出？<br />
<br />
因此，政治改革只有在两种情况下才有机会：1，政治合法性面临危机。在今天，最有可能的是政绩下滑带来的合法性破产危机，尤其是经济危机引发的社会危机；2，执政党“核心”权威破裂，内部分裂成多个山头，竞相争取下层及民间的支持，从而逐渐突破原来封闭的运行规则，开放政治参与。<br />
<br />
三，绝对控制与执法弹性<br />
<br />
从现代政治理论出发，绝对权力和绝对控制为我们勾勒出的是一幅万马齐喑、令人窒息的图景。但理论是灰色的，在现实中，中国社会在绝对权力的绝对控制下，正经历着长达30多年的经济增长以及社会其他方面的生动而有益的变化。对此，那些醉心于西方经典理论的虔诚学徒们，总是不知疲倦地从五光十色的现实中抽离出阴暗片断，试图削足适履地论证经典理论的普世价值，全然无视丰富多彩的整体现实。当理论面对新的对象、新的事实无法适应时，只能试图改变理论，而不能用有色镜或哈哈镜来扭曲事实。对中国正在发生的一切，必须有新的理论论证和理论模型予以解释。<br />
<br />
中国改革开放以来的经济发展和社会变化，首先应归功于权力的主导作用；准确地说，应归因于政治权力的执掌者恰巧是那些崇尚和致力于经济发展和改革开放的人。他们在对社会实行政治控制的同时，放松了其他方面的控制。放松控制释放出社会本身的活力，造成了经济及其他方面的显著进步，这不奇怪。奇怪的是，这种绝对权力下的放松控制，怎么会不与绝对权力本身所要求的绝对控制发生矛盾？<br />
<br />
这一难题的解决是借助于制度与操作、政策与执行的某种偏离来实现的。为实现绝对控制，政府除了把持政治权力外，还制订了涉及方方面面的严苛法令、政策和制度。如果认真贯彻这些条条框框，不但经济不可能有今天的增长局面，全社会也确实会陷入那种万马齐喑的窒息境地。但这些条条框框，政府从一开始就没打算认真执行。它们只是紧箍咒，作用是保证政府对社会的全面可控制性。在平时，为了培植和保持社会活力，这些东西被高高挂起，备而不用；只有当情况有可能失控，或者当政府需要调整某种做法时，才会把它念诵起来。政治权力则超越于这些政策、法令和制度之上，不受制约地推动经济发展和改革开放――这是人治社会独有的方便：既可轻易准备这么多政策、法令、制度，保证政治权力对社会的全面可控性；又可在对政权无威胁的情况下，将这些法令条文束之高阁，解放社会本身的活力。政府就是在这种“可控制而又不控制”的矛盾中，保持微妙平衡，既发展了经济，又维持了政权。<br />
<br />
在社会制度、政策法令，甚至国家指导思想的各个方面，我们都能看到这种严酷法规与执法弹性的巧妙结合。最经典的例子是“四个坚持”，如果认真坚持“四项基本原则”，中国今天可能比朝鲜还朝鲜。公开的规章制度要求每个人做圣人，但实际上，只要你不触犯权力，你大可不拘小节，活得轻松自在；在制度安排上，个人被剥夺了与政府讨价还价、自我保护的权利，但在现实中，你可相对独立地拥有自己的利益和空间，前提是不能对抗权力，危及政权。奥妙在于，你所享有的这一切自由、利益和空间，都是权力恩赐给你的，制度上对此没有保障；因此，如果需要，权力随时可将之收回；而所有成文不成文的法律制度，都是依据绝对权力和绝对控制的需要设计的，这是执政党为保证别无选择的合法性而加买的一重保险，这就解决了专制制度下放松控制的风险问题。执法上的弹性和灵活性，使人治政府能够集中足够的意志和力量，致力于经济发展和改革开放，为社会发展开拓空间。<br />
<br />
正因如此，各种为法治论者所看不惯的“严打”、“从严从重”等运动形式，以及强化税收征管等等，也就不足为怪。包括形形色色的“领导干部责任书”、“一票否决制”，都同理而异形：本来都是制度上早已明确的东西，但平时只供着，并没打算较真；现在临时要用了，就必须抱抱佛脚，强调一番，以示“这回要动真格了”。<br />
<br />
“以法治国”的困境也在这里：所有法律制度，都是依据绝对权力和绝对控制的需要而设的，其目的都是“治民”，因此，不可能期待它会反过来制衡权力本身。当然，对于行政权力而非政治权力，作一些具体限制还是可能的，但不可能形成法治局面――因为被法律限制的行政权力最终会在政治权力的缺口上逸出。这样一种人治下的非程序运作，必然逐渐削弱行政部门的行政效率，削弱法令政策的有效性。但是，它解决了当前怎样兼顾改革、发展、稳定的“三难”困境，创造了经典理论所不能解释的奇迹。可以说，在现阶段它居功甚伟。纵有流弊，从长期看，其负面效应也可能成为撬动集权体制的第一块砖头。<br />
<br />
四，经济信用与经济危机<br />
<br />
现代社会，经济有着不可比拟的覆盖率和重要性，对其他方面往往有举足轻重的影响。因此，即使在专门讨论政治问题时，也适于将其重点对待。<br />
<br />
如前所述，权力极力主导经济发展；权力放松对社会某些方面的控制，促进了经济增长，保持了30年的经济繁荣。但是在权力和市场的双重机制作用下，经济增长所创造的财富逐渐集中到少数人手上，贫富分化日益悬殊，社会底层贫困问题日渐突出，不但阻滞了经济的进一步增长、改革的进一步深化，并且已构成当今社会的突出危机。<br />
<br />
危机的突出只是一种表象，未必说明它即是社会问题的关键所在。实际上，只要经济增长能够持续，不但政权仍然能享有“政绩合法性”，政府也能拥有相对充裕的物质能力来缓和危机，将危机抑制在潜伏状态。<br />
<br />
因此，真正要命的危机是经济危机，危险就藏在中国政府至今引以为傲、依畀过重的“政绩合法性”中。只要经济危机爆发，社会其他方面所积攒的危机因素就再也没有转圜余地，必然随之整体喷发，政权合法性和社会稳定都将荡然无存。<br />
<br />
事实上，中国经济方面潜伏的危机已经很多、很重，对此，很多人已有论述。但是，这么多危机因素，为什么至今还只是蛰伏不发，没有走向临界点，爆发成真正的灾难呢？<br />
<br />
答案在于信心和信用。中国政府凭借其绝对权力和对大量经济资源的控制，抑制了各种负面因素和趋势的漫延，维持了国民经济稳定，从而维护了国家的经济信用和国内外对中国经济的信心。<br />
<br />
市场经济本来是敏感脆弱的，一有风吹草动，就可能蔓延成燎原之势。不过，它又具有某种自我修复功能，能够逐渐回到市场均衡。中国的情况不一样，市场经济远未规范成熟，内部蕴涵的不平衡因素太多，一旦针摆离开中心位置，很可能就再也回不来。在这种情况下，绝对权力又一次表现出它超越于市场规则之上的优越性：当市场上出现任何一点可能引发风险的波动时，权力马上介入，政府可以超越市场规则进行干预，或利用手中掌握的巨大资源作出弥补；当市场本身运转乏力时，政府又可以以自由经济所无法想象的方式和力度参入进来，成为加速经济运转的发动机。积极财政政策的最大意义正在于此：它挽救了中国经济的信用和信心。<br />
<br />
当然，这种打乱市场秩序的人为干预，必然损伤市场本身的发育和自我健全，但从整体和长远看，这是值得付出的代价――因为中国经济目前正运行在敏感区间，现阶段，只要能保持经济信用和信心，各种潜在危机就可能始终被抑制在临界点下，隐而不发；中国经济就可能挺过危险期，迎来一个相当宽松、有利于化解隐患的宽阔水面。反过来，如果一味强调市场经济理念和市场规则建设，一旦经济信用和信心被打破，各种危机因素失去制衡，它们将纷纷急于“兑现”，争夺残余价值，危机爆发将不可避免，且呈暴走状态。可见，绝对权力和绝对控制不仅是集权体制政治上的生命线；今天，它也是中国政府保持经济稳定的关键法宝，且是中国经济至今“一枝独秀”的奥妙所在。<br />
<br />
不能用西方国家经济危机发生的经验，来看待中国可能发生的经济危机。在西方，经济增长的停滞和衰退、经济危机的爆发，都是正常现象，是经济周期的自然运转。由于西方发达国家社会内部没有潜伏那么多尖锐复杂的矛盾，地面没有铺满干柴和火药，经济危机只相当于一次次小型火山的定期喷发，不但不会造成致命损害，反倒有利于整体环境的新陈代谢。但在中国，在遍地干柴火药的今天，经济危机的爆发极可能引发社会危机的总爆发；经济衰退可能成为引发整座多米诺骨牌倒塌的第一张牌，它将带动全社会的地壳剧烈运动，各种被抑制的危机都会争先恐后释放出来，社会将跌入不知怎样的黑暗和混乱中。<br />
<br />
经济危机会不会爆发，关键看国家经济的信用和信心能不能维持。有信心，暂时无法满足的利益和相互冲突的诉求都可俟之将来；纵然一时发生增长停滞甚至衰退，也不会酿成冲天火势；失去了信用和信心，增长纵然能维持一时，危机终不可避免。<br />
<br />
五，主动改革的可能性<br />
<br />
如前所述，当政权面临危机时，政改将有机会；当社会环境根本变化时，改革将水到渠成。但前者过于消极，后者将是一个漫长过程，在此过程中，今天我们很多人都将不在，许多利益或价值都已消散。因此，对不甘于现状的人们来说，最热衷的问题是：在当前，政治改革有没有主动推进的可能？<br />
<br />
无论从政治还是经济的角度看，要执政者放弃绝对权力和绝对控制，允许社会制衡力量的存在和生长，都是不现实的。唯一可以靠近目标的路径或许是：首先，民间仍然保持政治上的孱弱，不触及执政党的政治专利权，集中力量培植其他方面的基础力量；同时，政权本身作出改变，尝试培植新的合法性基础；最后，才是全社会的民主宪政进程。<br />
<br />
这就是“民主缓行论”所主张的由党内民主而党主立宪，最后实现虚党共和的民主宪政目标之改革路径。它首先不触及执政党对权力的垄断，不触及传统政治权力结构，而是顺应执政党在不危及政权的前提下谋求应付危机的办法，谋求新的政治制度合法性的要求，从政权内部入手，创造执政党内部的权力制衡，培植新的政权合法性基础和新型政治游戏规则。当政权内部新的基础和新的行为逻辑建立起来后，对社会的全面控制不再是政权之必需，政府才有可能真正放松控制，民间政治力量才可能生长发展起来。<br />
<br />
这个方案的局限性是，其启动最终有赖于执政党高层自我改造的主动性，外界只能提供某种压力和呼应。考虑到执政党视政权持续为第一需要，考虑到“政绩合法性”的不可持续，考虑到经济危机始终是中国社会高悬的达摩克利斯之剑，执政党自己启动“党内民主”的可能性很大，并且近期已有所迹示。民间和海外可做的是：以自己的言行，注释这种改革需要的迫切性。<br />
