													<h2>我读禁书的经历</h2>
						
						
																											<div id="postmessage_629222" class="t_msgfont">新人入会（以前在这儿潜水的，后被淹死。。。。^_^），发帖一篇，这是应一个在明报的朋友约稿写的，后来好像没有用上，发在这里吧。<br />
========================<br />
大学时代，我在网络上download到一个名叫『江主席发飙』的视频，和同宿舍的同学看，那个片段中，当时的党政军三位一体的江主席气急败坏的对香港记者说：你们那，too simple ,sometimes naive!我看的很好笑，而旁边的同学可不觉得好玩，他们一致认为：这是恶搞，是『演』的，不是江本人。我惊愕了几秒钟，看到了左上方翡翠台的台标。就算确认了这是段电视新闻，他们依然不肯相信一名国家主席会有这样的言行。这件事让我发现，我和他们生活在不同的世界，他们的世界是新闻联播和《环球时报》的世界，而我的世界是阅读一系列违禁书目和网页的世界。<br />
<br />
完全是出于对自己国家和民族历史的好奇，看完《走向共和》之后，拼命的找与之相关的书来读，像《非常道》《文武北洋》直到去年出版的《马关协定之伊李问答》结果一个个和我们历史书上所写的截然不同的历史人物浮现出来，人性之复杂果然是亦正亦邪，历史之诡谲果然不是简单的两分法可以概括。因此我感到信息对称和多元的重要，阅读《郑超麟回忆录》是更为有趣的经历，郑是早期党员，他专辟一章讲共产党人的恋爱史，将伟人的年轻时代的疯狂劲儿写的淋漓尽致，比小说还精彩，而以前这对我而言，都是『鲜为人知』甚至匪夷所思的。<br />
<br />
我看的最认真仔细的一本禁书是《鲜为人知的毛泽东》（Mao,the Unknown story)，从它英文版出版，就开始期待中文版，等了一年多，中文版出版，冒了风险托朋友从HK购得一本，一字一句看了两个多月，读书过程中，将许多内容一一向父母确认，完备而真实，因此对网上那些批判本书带着情绪和立场写传记的言论，颇不以为然，为毛大唱赞歌的大红色封面毛传，太多太多了。我必须要看看the other side story才敢相信天地间真是有这么一个人（or魔鬼）而不是神。我把书借给身边的同事看，他们也很喜欢，托人买了收藏，家史经历不同，他们更多是种猎奇的快意，没有我如此沉重。后来我又读她的《鸿：三代中国女人的故事》，依然感其故事，叹其才华，以家族史的角度来写社会变迁，仍是结构精美详略得当的no-fiction典范。前几天下载了同类型的齐邦媛的《巨流河》，还没看，不知是何风景。<br />
<br />
2008年3.14西藏暴乱时，所有目见耳闻的资讯，一面倒谴责达赖喇嘛，我想对西藏多些了解，于是看了《达赖喇嘛自传》，完全被达赖喇嘛的真诚气度和宗教情怀折服，从此，就算所有媒体将其称为妖僧，分裂祖国者，我再也不信，我只相信自己通过阅读他的自传从字里行间得出的判断。读毕，我详细的写了一篇读后感发布在读书社区豆瓣网，没过多久，就被管理方删除。可还是很多人看到了我的观感并发邮件索要电子版，我不厌其烦一一的发给他们，我很开心可以送人玫瑰手有余香。<br />
<br />
在这个神奇国度，我关于读书的种种荒唐经历之一是，各国的诺贝尔文学奖获奖作家的作品被翻译、炒作、热卖；中文写作的诺贝尔奖作家高行健的作品被列为禁书 。99年，大学暑假时在家翻出本89年的收获杂志，那一期有篇高行健的中篇小说《有只鸽子叫红唇》，我看的热泪盈眶，从此记住了高行健，那时，他还没得诺贝尔奖。后来，他的作品就被禁掉了。2008年，我第一次去香港，被香港的二楼书店深深吸引，逛的目不暇接，走的时候买了一套高行健的书，在地铁上开始看《一个人的圣经》打开就再也放不下，一口气读完。2009年，在网上看了书展的演讲录像，知道了哈金，很偶然买到《等待》的中文版，这本小说几乎是我看过的最好的小说了。像《黄祸》这样的小说不能正常出版，情有可原，可我至今仍不明白，为何连纯文学类书籍也遭禁，这么好的中文作品，为何我们无权欣赏。<br />
<br />
还有许多不错的书，一开始是获准出版的，后来莫名禁掉了，已售出的成了孤本，像《往事并不如烟》（即港版《最后的贵族》）董乐山译阿瑟.库斯勒《正午的黑暗》，说到这本书，还有件趣事，我在铜锣湾书店见到内地译林出版社的《正午的黑暗》被贴上三倍于原价的标签出售，忍痛买下，老板是个精瘦的香港人，戴着老花镜，见我拿这本书，目光从镜片后射出，严肃的说，这本书不错，我睇晒了，你知道是写谁的吗？我说，知道啊，写苏联共产党的嘛。他正色道：是写布哈林的。我至今仍记得他的表情和语气，深为感佩。<br />
<br />
一直被人问同一个问题：你看这些书有什么用呢？这个问题总是把我问的目瞪口呆，给不出答案。我不知道为什么，只是一直喜欢读书想要读到好书罢了，至于为何如此，我想大致是这样：忘记过去意味着背叛，历史本身就是中国人的信仰。而多年来学校教育和考试的文治武功，已经成功的完成了对每个中国人的社会主义唯物史观改造，而通过一天天一本本的阅读，书籍帮我撕下了这层崇高严肃的同时也是死板虚伪的面纱，还原一个个活生生的历史人物，猛然发现原来一群熠熠闪光、需仰视才见的大人物也会生气骂娘、聚众嫖娼，伟光正的革命者一样喜欢醇酒美妇一样利欲熏心阴险毒辣。这种散碎的阅读产生的力量，轻而易举的摧毁了看似固若金汤的主流史观，严肃沉闷虚假的历史烟消云散了。我感到历史的鲜活，感到一个丰富多元的世界。 <br />
<br />
不爱书的人不明读书之乐，更不明读禁书之乐，所幸像我一样喜爱读禁书的人也有不少，在网络时代，大家总能找到同道，就算没有同道，能与好书为伍，也不会感到孤单。</div>

							
							
							
							
