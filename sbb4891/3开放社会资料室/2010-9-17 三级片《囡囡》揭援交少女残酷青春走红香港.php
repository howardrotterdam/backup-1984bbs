《囡囡》在香港11间戏院上映一周后票房即突破100万港币。9月8日，影片监制伍健雄兴奋地在微博写道：“《囡囡》已经冲破一百万！迈向第二周！明晚诗雅、冼色丽落戏院现身派礼品及谢票解扣！请大家继续支持有勇气、有火的香港片！冲吖！”媒体踊跃报道，风助火威。<br />
