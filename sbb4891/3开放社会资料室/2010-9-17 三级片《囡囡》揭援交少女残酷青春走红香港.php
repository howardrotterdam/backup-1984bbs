<br />
最近，又有一部小成本香港电影备受关注，而且是三级片，名叫《囡囡》。<br />
<br />
该片讲述的是援交少女的故事，由新一代女演员诗雅、冼色丽等人演出。从本月2日在香港上映到今天，累计票房已近200万港币。该片监制伍健雄更是一路兴奋在微博发布消息：“《囡囡》已经冲破一百万！迈向第二周！”“明晚诗雅、冼色丽落戏院现身派礼品及谢票解扣！请大家继续支持有勇气、有火的香港片！冲吖！”“《囡囡》迈向第二周，累积票房冲向$150万！多谢大家的支持！”“囡囡解扣，票房不停”……高歌猛进的态势，让不少人叹称：《囡囡》有望成为“《志明与春娇》第二”(同为小成本的《志明与春娇》香港票房为600多万港元)。<br />
<br />
为何一部三级片会这么火？它仅仅只是情色意义上的三级片吗？昨日，该片导演毕国智接受南方都市报记者专访，畅聊戏内戏外的种种。《囡囡》是这位第25届“香港电影金像奖”最佳新晋导演奖得主的最新力作，继《海南鸡饭》、《战・鼓》后镜头直指80、90后援交少女新面貌，引得全城注目。<br />
<br />
题材火爆<br />
<br />
聚焦援交少女，直指社会问题<br />
<br />
“援助交际”一词起源于日本，最初指少女为获得金钱而与男士约会。《囡囡》此次聚焦香港援交少女，片名中的“囡囡”是对小女生的昵称，藉此带出“学生卖春”这个日渐严重的社会问题。据香港媒体此前报道，援交少女不一定身世可怜。调查发现，很多就读名校、成长于小康家庭的学生，为赚钱买名牌及毒品而沦落风尘。有专家称，不少援交少女直至感染性病、怀孕才会求助，媒体的调查结果也只反映了“冰山一角”的情况。导演毕国智以此社会热议话题为电影题材，意图引起广泛的社会反响。《囡囡》选取了四位典型的援交女的独特经历，她们动机各不相同，有的为赚钱买名牌，有的为网上猎男，有的为寻找爱情。她们都被用地道的行话呼之，如鱼蛋妹、老泥妹、凤姐等。<br />
<br />
【导演阐述】拍我们自己想拍的<br />
<br />
谈起筹备拍摄此片的心态，毕国智向南都记者强调两点：一是对香港电影关注性工作者这类题材的继承；二是在香港大批知名导演北上拍片的大背景下，年轻导演需要大胆去做，“不一定要做市场需要的，而是拍我们自己想拍的。”<br />
<br />
“香港每个年代都会有讲述妓女的电影，根据当时的社会情况，各有不同的方式。”毕国智说，他所观察到的是，这个时代的情况与以前大不一样，“以前，很多妓女都是被强迫的。如今，很多援交少女是要赚钱，赚钱去玩，边玩边做，有时，玩乐的性质多过于工作。还有的是富家女，玩得很疯狂。比如，她们会先通过网络结识男生，约了碰面之后如果不满意，就不玩。这也是一个与之前不同的地方，现在女生懂得掌握主动权，不是男选女，而是女选男。”为了尽可能地把握这个群体的心态，在准备初期，毕国智还“以身试法”在网络上与她们对话，“我research的方式是，注册账户，直接登录成人网站的援交版面，add一个囡囡的m sn。实际情况比我想像的要快不少，对方马上sayhi，我也hi，对方讲话简洁准确十分专业，全是一个单词：trade？place？tel？”<br />
