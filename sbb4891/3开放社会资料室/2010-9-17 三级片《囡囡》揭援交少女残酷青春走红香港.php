“《靓妹仔》过去20年后，《囡囡》重拾衣钵，将Me时代的援交女孩题材搬上银幕。四个援交‘囡囡’，或空虚，或迷茫，或是为情所伤，或是寻找真爱。她们自主选择，拥有与男性同等的欢爱权利。电影既不批判亦无平反，旨在勾画一个现实得残酷的社会现象。”<br />
