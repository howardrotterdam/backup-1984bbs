八月初，《囡囡》就公布了一组宣传海报，其中两款尤为抢眼，主演冼色丽和诗雅神情忧郁背靠而立，两人都手执内衣扣欲解还休的样子，标注的宣传语“9月2日囡囡解扣”。此外海报上还印出了援交少女的基本情况和各类“服务项目”收费标准，以及标语“不要理我，尤吮莆摇薄敖袢M e时代援交少女，我对出钟无悔！”等。海报在香港闹市张贴，吸引眼球。<br />
<br />
由于题材敏感，《囡囡》较早于第34届香港国际电影节做首映时引起极大回响。也因为尺度太大，该片没法引进内地。女主角冼色丽曾对媒体表示：“电影上映的消息在微博上发出后，看到网友的正面响应，真的非常兴奋。最可惜的是因为题材问题，《囡囡》不能在内地放映；很多内地网民均在微博上留言，有点感激又遗憾。”<br />
