																											<div id="postmessage_554181" class="t_msgfont">第二，红卫兵之外的群众组织研究。<br />
90年代以后，由于对群众组织研究的越发重视，学者的目光超出红卫兵的范围，把研究对象扩展到学生之外的其他社会群体，对文革中的工人、市民组织均有不少研究，拓宽了研究的领域，从而将文革研究引入更深层次。<br />
王绍光在《理性与疯狂：文化大革命中的群众》（Failure of Charisma: The Cultural Revolution in Wuhan）中对包括红卫兵组织在内的所有群众组织都有描述，如武汉文革的主力军――“毛泽东思想战斗队武汉地区工人总部”（简称为“钢工总”），“工总”的同盟、武钢及一冶产业工人组成的“毛泽东思想九&#8226;一三战斗兵团”（简称“钢九&#8226;一三”），“武汉工人革命造反总司令部”（简称“工造总司”），“百万雄师”等等。经过分析，他认为不同社会集团的人心中的领袖形象不同，由此导致了不同的行为方式。运动中的人们对毛指示的解释并不真正关心其“本来的”或“真正的”含义，他们的解释实际上是个人背景、愿望和需求的产物。 王芳在文献资料基础上，围绕普通工人为何及如何参加武汉“钢工总”，以及“钢工总”组织如何运动的问题进行了广泛的口述历史调查。《记忆中的底层文革――关于文革时期武汉“钢工总”的口述历史研究》一文阐述了“钢工总”的产生和崛起过程，分析了组织领导人和普通成员的造反动机，理清了“钢工总”与其他群众组织的关系，剖析了“钢工总”内部结构，介绍了其章程和制度、财务状况和领导更替情况。 <br />
李逊原为上海总工会工作人员，利用编写《上海工运志》的机会，接触了上海工总司的档案材料，并用心作了收集和记录，写了《大崩溃：上海工人造反派兴亡史》一书。该书主要以上海工人造反派组织“工总司”的造反史为线索，用3/5的篇幅详细记述了1967年8月4日“踏平联司”之前“工总司”的崛起和发展。该书资料丰富，许多分析颇有见地。 李逊与裴宜理合作完成的《无产阶级的力量：“文革”中的上海》（Proletarian power：Shanghai in the Cultural Revolution）一书1997年出版，是又一本关于上海文革的专著。全书共七章，主要研究文革时期上海的群众组织――工总司、赤卫队、红卫兵、闹“经济主义”的群众组织以及写作组等的活动情况及其特点，对一些重要人物如王洪文、潘国平、王秀珍、耿金章、等进行了介绍和分析，也专门讨论了一些重要事件如安亭事件、《解放日报》事件、上海“人民公社”事件等。裴宜理在《上海罢工：中国工人政治研究》中，研究解放前上海工人运动后提出了一个核心观点――“不同的工人有不同的政治”。 《无产阶级的力量：“文革”中的上海》打通了解放前和解放后，观察到了工人状况的延续和变化，认为上海文化大革命的历史和其它城市的历史有很大的不同，关键是工人的作用和角色，上海的工人在文化大革命中有不同的社会取向――造反派向党的权力要权，保守派相反，闹“经济主义”的群众组织则要求社会经济的公正待遇。她认为很特殊的情况在于，当军队在全国对群众组织已经取得控制权时，上海的核心政治权力仍由造反派控制，上海的造反派实际上是张春桥、姚文元、王洪文等文革激进派的社会基础。 <br />
刘国凯《广州红旗派的兴亡》一书详细记录广州造反派组织“红旗派”和保守派组织“东风派”在1966――1968年间的争斗全过程。该书深入群众组织内部，分析了组织成员的构成，指出广东省的文革运动主要集中在城市，广州重工业少，工厂规模有限，60年代的工人中，其父辈也是工人的很少，他们的家庭出身多是小商小贩、个体手工业者、职员、技术人士等，因此在造反派中其非红非黑人数最为强大，这也是文革期间大中城市造反派能居优势的原因之一。该书还注意到“红旗派”里有一部分是支青队伍，他们加入红旗派目的非常明确，就是希望借助造反回城就业。 作者还分析了不同社会群体各自的政治和经济诉求，认为正是不同层次的诉求导致了“红旗派”内部的分歧。 <br />
叶青认为福建群众组织的对立与演化是毛泽东所领导的文化大革命统一历史进程中的一个方面，是与整个文革的历史进程相适应的。群众组织间对立与演变，即某个组织合与分、分与合、分分合合都是错综复杂的，诸如中央政治气候的变动、各个组织（人群或阶层）素质、历史因素、现实利益、文革本身、本地区特点，等等，甚至是诸因素兼而有之。 赖正维在《“文革”时期的农民“造反”组织及其活动管窥――以福建为个案的分析》一文对文革时期福建农民组织的建立、活动和影响有所描述。文章认为，农民为主体的群众组织的建立与红卫兵大串联有关；大多组织庞大，分工细致，设置严密；组织成员的行为动机主要是响应毛主席号召、同修正主义路线作斗争，但不可否认也夹杂着诸多的其他因素。 金大陆对上海文革中的红卫兵小报、群众报刊等“宣传品”的种类、出版情况、发行渠道作了详实的记述和分析，对研究上海文革中的群众具有独特的价值。</div>

							
							
							
							
