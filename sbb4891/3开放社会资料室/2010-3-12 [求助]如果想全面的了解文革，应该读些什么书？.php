																											<div id="postmessage_554176" class="t_msgfont">大陆之外的学者也有不少文革通史著作。文革期间，中国与世界隔绝，但世界并未忘记中国。文革爆发后，西方学者注意到这一不同寻常的政治运动，开始着手收集资料并陆续有文章面世。美国政府及其情报部门也密切注视中国的动向，开展了持续的情报收集工作。 然而，当时连身在其中的中共党内高层领导都因为不理解而跟不上形势，更加不用说万里之外大洋彼岸的外国学者了。<br />
但有部分学者显示出敏锐的问题意识和惊人的洞察力，研究成果一问世即引起广泛关注。麦克法夸尔（Roderick Macfarquhar） 从1968年将文革作为自己的主要研究题材。1974 年，他发表了“文革前史”三部曲的首卷《文化大革命的起源：人民内部的矛盾，1956――1957年》；1983 年，发表了第二卷，聚焦大跃进；1997 年，第三卷发行，剖析中共高层在 1961-1966 年的严重分歧。 三卷文革起源的专著奠定了麦克法夸尔在文革研究领域领军人物的地位。 <br />
邹谠（Tang, Tsou）在《文化大革命和后毛时代的改革》（The Cultural Revolution and Post-Mao Reforms: A Historical Perspective）一书中运用政治科学的多种方法――包括系统分析法和理性选择理论――以政治科学的理论框架来分析包括文革在内的历史事件。该书第一章题为“革命、重建和共产主义中国的危机”尝试建立起分析的理论框架；第二章“文化大革命和中国的政治系统”体现出鲜明的系统分析风格，认为文革的根源在于毛泽东的思想意识和变化的社会现实之间日益扩大的分歧；第三章“中共党内派系政治”考察了党内高层领导之间的分歧。 其余各章在文革研究的基础上对后毛时代的问题进行了分析。总的来看，作者侧重讨论的是意识形态和政治发展之间的相互影响，但他指出意识形态并不是影响20世纪中国复杂的社会政治发展过程及其结果的唯一因素。为支持这个观点，他不仅考察了意识形态，而且考察了历史传统、国家与社会关系、政权类型与组织机构、马克思主义在中国的不同阶段及其他一些因素。<br />
麦克法夸尔（Roderick Macfarquhar）和费正清主编的《剑桥中华人民共和国史：中国革命内部的革命(1966――1982年)》是海外文革通史中的高水平著作。该书第二至五章用精炼的语言记录了文革的过程，还另辟专章分析了1949年至1976年的毛泽东思想（第一章），文革期间的经济政策及其贯彻情况（第六章）、教育（第七章）、文艺创作与政治（第八章），共产主义统治下的农村（第九章）、城市生活（第十章）、文学（第十一章）。该书的资料来源极其广泛，有效地借鉴吸收了60年代以来的研究成果。遗憾的是对文革过程的叙述篇幅不够。 2006年，文革爆发四十周年之际，麦克法夸尔（Roderick Macfarquhar）和沈迈克（Michael Schoenhals）合著的《毛泽东最后的革命》（Mao ’s Last Revolution）出版，该书弥补了剑桥史的不足，对文革历史作了全景式的描述。 此书涵盖了文革历史的整个过程，囊括了文革的各个阶段，具体地凸显出毛泽东对文革的独特作用这一主线。此书不仅仅限于文革的达官显贵或风云人物，还把关注的目光投入平民百姓的文革生活，以鲜活的话语和真实的记述从不同的角度映现出文革奇异多样的风貌。此书填补了文革研究中长期存在的几个空白，最主要的就是对“中央专案审查小组”和“清查5&#8226;16”的揭露和概述。 全书注释共1900多条，几乎所有的资料都经过了严谨的考证并列出了来源。 <br />
2008年，港版《中华人民共和国史》先后出版，目前已经出版了六卷。该套国史时间跨度由1949年――1981年，大致为一卷涵盖三年时间。阅读了最先出版的三卷后，陈永发评价说：“有丰富的文献档案为基础，史实的可信度极高。”余英时说：“每卷都达到了史学著作的世界水平”。 十卷国史中，直接涉及文革为第六卷卜伟华所著《砸烂旧世界――文化大革命的动乱与浩劫(1966――1968)》，第七卷高华著《新秩序和新冲突――从中共九大到林彪事件(1969――1971)》(尚未出版)， 第八卷史云、李丹慧著《难以继续的“继续革命”――从批林大批批邓(1972――1976)》。 卜伟华在第六卷中，针对过去国内文革研究著作和论文粗疏、大而化之的缺点，以时间为经，以事件为纬，不回避，不省略，不跳过，娓娓道来，处处使人看清了事情的来龙去脉、前因后果，给人以完整的印象。 第六、七、八卷国史可构成迄今为止最详细的文革通史，标志着反思和研究文革取得了长足的进步。与已有的文革著述相比，国史在史料整理、史实考证、史识方法和观念批判等等方面都有新的、质的飞跃，为结束“文革在中国，文革学在国外”的局面迈出了扎实而可喜的一步。</div>

							
							
							
							
