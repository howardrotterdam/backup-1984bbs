																											<div id="postmessage_554184" class="t_msgfont">第三，文革与社会矛盾。<br />
“文化大革命”从根本上讲是一场政治运动，其核心问题是政治性的矛盾――党内斗争，但是一些复杂激烈的冲突也反映了社会性的矛盾。<br />
林怀特（Lynn T. White）的《政策混乱：文化大革命中暴力现象的组织原因》（Policies of Chaos: Administrative Causes of Violence in the Cultural Revolution）将1949年作为研究的时间起点，以上海为例分析了镇反、合作化、反右、大跃进、国民经济调整、夺权、武斗等历史事件和工人、学生、市民、干部等多个社会群体在各个阶段的反应；认为文革是先前已存在的社会情形的自然继续，根源于极权模式（totalitarian model）及与之配套的社会管理制度，文革中的混乱和暴力是中国社会矛盾共同作用而产生的；社会矛盾的三种来源分别是把人分为三六九等使人们具有不同的政治地位，单位中群众和领导的人身依附关系和周期性的政治运动使得暴力合法化。 <br />
印红标在《“文化大革命”中的社会性矛盾》一文中考察了1966年至1969年的历史事实，从三个方面阐述了自己的观点。第一，知识分子问题与社会矛盾。毛泽东发动“文化大革命”的一个重要原因，是对文化教育界和知识分子状况的强烈不满。“文化大革命”的重点是整所谓“党内走资本主义道路的当权派”，在知识分子中批判所谓“资产阶级反动学术权威”，进行文化教育等上层建筑领域的“革命”，是第二位的任务。这不仅是政治斗争，而且在相当程度上展现了带有社会性的矛盾即领导层与知识分子的矛盾。第二，群众运动与社会矛盾。在具有高度集中的政治经济体制的社会中，领导干部与群众的矛盾是植根于现实的、普遍存在的社会性矛盾。在缺乏有效协调机制的情况下，一遇机会，群众平时积聚的对领导干部的不满就会以激烈的形式表现出来，例如1957年整风鸣放、1963至1966年城乡社会主义教育运动和文革中的情况。第三，政治斗争与社会矛盾。文革期间，群众与领导层的社会性矛盾被纳入党内斗争的轨道，但干群关系远没有尖锐到导致政治运动的地步。所以一般群众不满领导的官僚主义压抑、特殊化、特权等弊病，又真诚地对领袖、对执政党寄予希望，在思想上易于接受以旧制度阶级关系解释新制度社会矛盾的观点，以无产阶级和资产阶级的阶级斗争解释新制度下的社会矛盾。另外，毛泽东个人崇拜的盛行。群众相信毛泽东的正确性，习惯于在他的思想框架内寻求扫除社会不公正，创造理想未来的出路。 <br />
王绍光认为是探寻平等和追求发展两者之间的基本矛盾给社会主义中国提供了内部政治的动力，并产生了最终导致文革的典型特性和压力。这个矛盾在三个层次上展开――错综复杂的政策侧重点的变换，领导层的分化和无所不在的社会冲突。他指出，作为社会的两个组织原则，精英统治和平均主义取向各有其特殊的组织目标，各有不同的手段，并有明确的社会力量的支持。任何政策上的转变必将使一些人感到满意而同时加重另一些人的不满。政策倾向上侧重点的复杂变化，使不同利益的社会团体感到他们彼此总是处于对抗之中。对于地位、权力和稀有资源的估价或要求，或明或暗地存在着持续不断的冲突。文革前的中国有三个重要的社会分化，精英与群众间的垂直冲突，精英之间的水平冲突，群众之间的水平冲突。在文革前的中国，社会冲突导致了紧张、失意、缺乏安全感和不满情绪的增长。没有一种使紧张得以放松的机制，就有可能来一场大爆炸。 <br />
李逊与裴宜理的《无产阶级的力量：“文革”中的上海》（Proletarian power：Shanghai in the Cultural Revolution）对上海出现的“经济主义”浪潮有细致研究，所得的结论王绍光的看法有一定契合。 “工总司”也就是造反派，“赤卫队”是保守派，另外还有一派叫做“经济主义风”。这三部分工人运动具有很大差别，特别是他们的斗争目标不一样――“工总司”的造反派主要目标是为了推翻掌权者，保守派的“赤卫队”是为了维护当时的掌权者，“经济主义风”这一派主要是为了改变他们自身的经济状况。他认为，前两者具有政治目标，第三派“经济主义风”则是真正的无产阶级，主要是一些合同制工、临时工等等；他们的主要目标是为了改善自己的经济处境。“上海临时工、外包工革命造反总司令部”迫使中共上海市委，中共中央华东局及上海市劳动局的代表签字同意他们的六条要求，就在政治内容之外，涉及到用工制度。产业工人直接指向了经济利益。部分学徒工反对学徒期延长，到主管单位造反，要求补发转正的工资差额。各单位职工找种种理由要求发放交通费、早餐夜餐费、加班费、洗澡费、工作服装、工作手套、工作鞋、洗脸毛巾、肥皂等等。可以看到，裴宜理特别注意运动参加者群体间的区别。 <br />
第四，政治文化、群众心理和社会思潮。<br />
陈佩华的专著《毛主席的孩子们――红卫兵一代的成长和经历》（Children of Mao: Personality Development and Politically Activism in the Red Guard Generation） 最早尝试揭示文革前个人政治社会化过程。该书通过对11个人的深度访谈，选取四个代表人物，划分为顺从的、有纯正癖的、反抗的、讲求实际的四种积极分子类型，考察了他们小学时期的政治教育与性格形成的关系、青春期的政治社会化、在文革期间的表现和文革中后期的变化，讨论中国的政治社会化与权威人格。他认为新中国青少年的政治社会化过程中，国家对价值观形成和行为的影响具有深远意义，尤其是那些前政治积极分子；家庭在价值观形成方面的作用十分微小，儿童们对党、国家、毛泽东和英雄模范的情感取向部分地代替了对家庭的情感取向。同辈群体之间的竞争压力发挥了很大的作用。 <br />
随着研究的细化和深入，从文化、心理层面进行思考的成果大量出现。唐少杰《从清华大学“文革”个案谈红卫兵运动的失败、情结及评价问题》文章指出，红卫兵运动有“造反有理”情结、准救世主情结、革命实用情结、泛敌意识情结、迷信领袖情结、暴力崇尚情结、血统出身情结、道德专政情结、英雄主义情结、理想至上情结等，这些“红卫兵情结”不是形成于文革之际，而是与文革的孕育和发动密切相关；红卫兵的孕育和形成于新中国的历史密切相关，“红卫兵情结”为一代红卫兵带来沉重的历史抱负，但是红卫兵运动也是20世纪中国青年运动中最大的一次破产或异化。 《一叶知秋：清华大学1968年百日大武斗》一书也涉及到清华大学造反派的思潮论争。 <br />
近年来，关于社会思潮的研究不断有成果出现。<br />
王绍光的《文革中的新思潮》（‘New Trends of Thought’on the Cultural Revolution）一文重点考察了“新思潮”的代表――湖南“省无联”和湖北“北决扬”。“新思潮”的观点认为文革的目标不是改组官僚政治，而是要创造以巴黎公社为模型的新社会。革委会的建立顶多只是一种表面改革，真正重要的是创造一种新的生活方式，一种没有压迫和剥削，没有官僚政治和经济上及其他方面的不平等的生活。 这种“新思潮”在文革最初的几个月就已出现，伊林&#8226;涤西的公开信是“新思潮”初试啼声。随着时间推移，“新思潮”成为一种普遍现象，尽管从未成为造反势力的主要倾向。其中最具代表性的是湖南的“省无联”和湖北的“北决扬”。他们试图根本改变现存的社会和政治结构，但由于他们缺乏历史、哲学、经济的基础知识，所以只能从经典著作中摘取只言片语来支撑自己的政治纲领――巴黎公社之所以成为他们所推崇的模式，是因为这是他们所知道的接近其理想的唯一模式。同时，他们也从未思考过巴黎公社实际上是如何发挥作用的。 <br />
《文化大革命和它的异端思潮》一书收集了“血统论”、“出身论”、“怀疑一切论”、“四三思潮”、“五&#8226;一六思潮”、“省无联思潮”、“北决扬”、“四&#8226;一四思潮”、“李一哲思潮”等社会性思潮的原始文本。该书高度评价“出身论”，认为它是追求人权、追求平等权利的杰出代表；而“李一哲思潮”承递了遇罗克关于政治权利平等的追求，并进一步把这一诉求推进到健全法制的新的高度。其次，该书认为“省无联思潮”、“北决扬”是从左的方面追求民主，追求人民群众对于国家实际的管理权利，形成了独立于官方理论之外的反特权、反官僚的异端体系；“联动思潮”、“四&#8226;一四思潮”等则是从右的方面抵制文革的思潮。作者认为，无论左的还是右的异端思潮，都已在文革中汇成了一股挑战与颠覆正统的合力，是文革留下的思想财富之一。 <br />
印红标在研究红卫兵的过程中对青年心态、思想和社会思潮等问题有所论述。他与唐灿、米鹤都、陆建华的主题对话梳理了红卫兵及其同时代人的思想轨迹。几位学者提出红卫兵是文化革命前十七年教育结下的一颗苦果，他们率先卷入文化大革命的原因在于需要从心理学、社会学等多个角度加以说明，重要因素之一是文革前十七年社会环境对青少年的教育和熏陶，他们思考所依据的理论是错误的。 在文革的的各个阶段，红卫兵群体的思想和行为都有转变。最后，他们上山下乡，正式步入社会后由于人生阅历的丰富思想也因此发生了更多的变化。 印红标较早的文章还有《坚冰下的潜流：文革中的知识青年思潮》 和《抗争者的冲突：遇罗克与联动的论争》 等，其在思潮方面研究的集大成者是专著《失踪者的足迹――文化大革命期间的青年思潮》。 该书对文革十年先后出现在老红卫兵、保守派红卫兵、造反派红卫兵、上山下乡知识青年及其他青年群体中的一些或显（如联动思潮、极左派思潮、“新阶级”思潮、李一哲大字报代表的思潮、四五运动前后的各种思潮等）或隐（当时并未公开发表或遭到批判从而没有得到传播造成影响）的重要思潮个案都尽可能地搜寻、荟集起来，作了客观的记叙与冷静的评说，对不同时期的重要青年思潮，作了分门别类的发掘和梳理，资料之丰富，涉猎之广泛，堪称一部文革十年青年精英政治思潮的“小百科”。 <br />
专门研究遇罗克个人的资料有徐友渔等主编的《遇罗克遗作与回忆》 和遇罗文的《我家》 等。此外，杨健对文革“地下文学”的研究 和徐晓主编的《民间书信》 都从不同侧面对青年思潮有所反映。</div>

							
							
							
							
