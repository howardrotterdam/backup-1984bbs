<br />
一 相关研究述评<br />
1 文革通史类著作<br />
国内比较早的文革通史著作有高皋、严家其的《文化大革命十年史》 和王年一的《大动乱的年代》 ；后有金春明的《“文化大革命”史稿》 和席宣、金春明合著的《“文化大革命”简史》。 <br />
《文化大革命十年史》力求“探讨文化大革命产生的原因，研究文化大革命进程本身的规律性，寻找出防止文化大革命这类动乱以新的形式产生的原则和方法”。 全书分三篇：第一篇“需要更多的个人崇拜”；第二篇“林彪的崛起和覆灭”；第三篇“江青和文化大革命中的政治”。简而言之，该书是从毛泽东与三个人的关系角度来阐述整个文革的历史过程――第一篇主要内容围绕毛刘关系，第二篇是毛林关系，第三篇是毛江关系。从具体内容而言，该书没有实现作者的意图――对原因探讨很少，事实描述的工作也不完备。所以，王年一评价说：“一部文化大革命史本来应该系统地说明左倾错误的形成、发展、危害、教训等等，”但这本书“在这方面有严重的欠缺”， “有不少缺点，比较粗糙”。 <br />
金春明发表过几十篇相关文章和小册子，《“文化大革命”史稿》是其思考研究成果的总汇。全书分五篇，分别是山雨欲来（文革之前），狂风折梁（文革初期），欲罢难休（文革中期），风波又起（文革后期），余波难平（文革之后），研究时段不局限于1966――1976年，而以50年代农业合作化、八大起笔，以80年代真理标准大讨论和《决议》彻底否定文革收笔，时间跨度为30年。该书从探索社会矛盾的角度，尝试把文革放在更长的历史背景下考察，有助于把握文革的前因后果。《史稿》在文革起因的分析方面具有贡献，总结了导致文革产生的三个恶性循环――“左”倾理论同“左”倾实践的恶性循环；集权体制同个人崇拜的恶性循环；国际反修同国内反修的恶性循环。 这一分析为从整体把握文革的原因作出尝试。此外，该书在问题阐述和史实评价方面不时体现出作者思想上的个性，在大陆文革研究领域尤为可贵。书中没有遵循其他一些论著把文革的全部或主要罪过归咎于林彪集团和江青集团的简单做法，在许多问题上坚持了具体问题具体分析，向读者表明文革中的重大事件乃至文革本身，没有毛泽东的首肯和决定是无法进行或无法出现的。比如，在谈到文革发动过程时，该书写道：“打倒罗瑞卿是林彪同毛泽东的一笔政治交易。” <br />
但该书对文革本身的书写，尤其是1967――1968年的情况描述却显得不足。唐少杰批评说：“史稿对1967年初春至1968年底的文革众多重大事件辍笔而过，对在此期间的许多群众运动、群众组织、群众派别斗争和全国性的诸多群众思潮忽略不计。” 文革的问题离不开群众问题，必须正视群众问题在文革中的地位和意义，确定群众问题在文革中的角色和功能。否则，文革作为一场亿万人民群众参与或置身于其中的运动就无从谈起。对文革中的群众问题重视不够，评说不足，特别是对1967年到1968年状况的评述显得薄弱甚至留有空白，不能不说是史稿最大的缺陷。<br />
席宣、金春明合著的《“文化大革命”简史》被认为是一部进入理论思维的史学著作。该书对历史过程的叙述较为简略，作者力求把读者带到理性分析、冷静审视的境界。该书大致承袭了金春明在《“文化大革命”史稿》一书中提出的“三个恶性循环论”，提出文革“不是真正的革命群众运动”。亿万群众卷入文革的原因有四：一是出于对中国共产党和毛泽东的信赖；二是50年代后期在党内滋长的“左”倾思想对干部和群众产生了广泛影响；三是政治思想上的强大压力，运动中人们被迫选择表明态度作出抉择；四是林彪、江青、康生一伙野心家、阴谋家乘机蛊惑挑唆、蓄意制造动乱，煽动一些不明真相的群众。所以，文化大革命不是广大群众的要求和需要，而是一场由领导者错误发动，被反革命集团利用，给党、国家和各族人民带来严重灾难的内乱。 <br />
张化对该书秉持的“三个恶性循环论”和“文革不是真正的革命群众运动”观点表示赞成，且对“三个恶性循环”提出了修正和深化的建议。 也有学者认为既然书名为简史应该着重叙史，但本书对历史过程的叙述过于简略，对一些重大事件的叙述缺乏足够的史料。另外，还有学者指出《简史》过于注重政治运动或上层政治活动的介绍，而对社会层面、群众层面观照不够，应该对群众心理、群众活动、社会政策、制度沿革等领域的研究分析。 唐少杰对该书评论说：“简史忽略或回避了人民群众的内在的需要、要求和利益(如利益驱动和利益分化)以及人民群众活动的自发性、自主性等问题，亦即很容易把文革中的人民群众看成是完全被动的、盲目的。如同过去一些关于文革研究的论著那样，简史没有给予文革中的群众问题充分的、全面的、切实的论述和评价，群众问题所占的篇幅可谓是微乎其微。这不能不说是简史一书的最大不足。” 在他看来，人民群众最实际、最深切的生活和感受等等被很多论著置于可有可无或完全忽略不计的地位是不合适的。<br />
之所以将《大动乱的年代》放在最后评述，不是出版时间晚于上述几本著作，而是因为从整体水平上来看，上述著作没有超出1988年出版的《大动乱的年代》。全书分三个部分，第一篇“全面内战，打倒一切”叙述1966年5月――1969年4月期间的事件；第二篇“新的斗、批、改和林彪事件”书写1969年――1973年8月的历史；第三篇“肯定与否定文化大革命的斗争”讲述1973年――1976年10月的过程。该书对文革中的事件及其细节有比较详细的描述，对于一些群众组织及其活动也有论及（很多问题前几本著作均未涉及）。王年一先生是文革研究的开拓者和奠基人，这本《大动乱的年代》是目前最权威的文革通史，得到了海内外学者的高度评价。以至于麦克法夸尔在2009年仍然说：“在我看来最好的中文文革著作是王年一的《大动乱的年代》。” 当然，因现实环境的困扰和作者当时认识的局限，这本著作也有种种缺陷。作者后来曾经说：“今天回过头来看看旧作，字数不少，发现有不少缺点。……‘资产阶级反动路线’的错误，‘联动’的错误，当时我就没有认识到。” 不可否认，该书大大拓展了文革研究的广度和深度，其卓越的贡献有目共睹。 <br />
另外，在各种中国人民共和国通史著作中，对文化大革命多有专章论述。郑谦、张化的《毛泽东时代的中国（第三卷）》也是一本文革通史，记述了十年文革的全过程。该书具体翔实地记载了诸多细节问题，如辟出小节来谈“斗、批、改”、“清理阶级队伍”、“整党建党”、“教育革命”等问题。 其他著作还有红旗出版社1994年出版的《中华人民共和国历史纪实》中有《内乱骤起(1965――1969)》、《风云激荡(1969――1973)》、《极左哀秋(1973――1976)》将1966――1976年的十年历史分三个阶段进行了记述。 还有《中华人民共和国专题史稿》第三卷《十年风雨(1966――1976)》收录了多位学者文章，以专题形式对文革的发动、红卫兵运动、“三支两军”等问题有精彩论述。</div>

							
							
							
							
