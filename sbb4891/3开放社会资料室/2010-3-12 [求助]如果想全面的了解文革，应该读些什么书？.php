																											<div id="postmessage_554188" class="t_msgfont">第五，个人文革史和地区文革史<br />
文革以后，除了党和国家领导人的传记之外，大量其他人的自传或回忆录也得以出版。文革一段的经历无疑是这些著述的重头戏。关于陈伯达的回忆类书籍先后有《陈伯达遗稿：狱中自述及其它》 和《陈伯达最后口述回忆》。 中央文革小组另一成员王力在香港先后出版了《现场历史：文化大革命记事》和《王力反思录》。 原上海市委书记徐景贤写的回忆录《十年一梦》由香港时代国际出版有限公司于2003年12月出版。原外交部工作人员马继森的回忆录《外交部文革纪实》于2003年在香港中文大学出版社出版。2004年，《吴德口述：十年风雨纪事――我在北京工作的一些经历》在北京出版，吴德曾先后任北京市长、市委第一书记。 2005年，《陈丕显回忆录：在“一月风暴”的中心》 在上海出版，陈在文革爆发前后任上海市委第一书记。曾任江青秘书的阎长贵和中央文革小组办事组组长的王广宇合作推出接近回忆录性质的文革研究专著《问史求信集》，对自己所了解的高层人和事的情况有详尽交待。 文革“五大领袖”之一的聂元梓所著回忆录2009年在香港出版。&nbsp;&nbsp;<br />
近些年来，越来越多的普通人将自己的回忆类文章和自传付梓，得以让更多的人了解到普通人在文革期间的状况。同时，因为普通人的“文革回忆”侧重于个人在文革中经历，所以这些回忆录通常也成为个人视野中的地方文革史。徐友渔的《蓦然回首》即可视作其个人的“文革回忆录”，也可以视为其所观察到的四川文革史。 前文提到的一些红卫兵，先后推出了自己的回忆录，周孜仁的《红卫兵小报主编自述：中国文革四十年祭》是对四川文革情况的反映，作者曾是四川重庆“重大八&#8226;一五战团”的头号理论家，担任过《八&#8226;一五战报》的主编。 陈焕仁的《红卫兵日记》 和宋柏林的《红卫兵兴衰录：清华附中老红卫兵手记》 是北京文革史研究的宝贵资料。鲁礼安是“新思潮”中“北决扬”的代表人之一，其回忆录《仰天长啸：一个单监十一年红卫兵狱中吁天录》 对武汉地区文革和“北决扬”都有详细记叙。吴焱金口述，钟逸整理的《四十三年望中犹记》也是武汉文革史的重要资料。吴焱金文革爆发时是武汉市无线电元件厂工人，文革中参加造反任武汉“工造总司”勤务员。1967年6月，“7&#8226;20事件”前夕任武汉“揪陈抗暴总指挥部”副总指挥。8月下旬任“新派”联合成立的“武汉市无产阶级革命派总司令部”总指挥，后任武汉市革命委员会副主任、武汉市总工会副主任，是武汉文革的风云人物之一。此书从一个青年工人、造反派负责人的角度对武汉文革历史作了详细回忆。 另外，王绍光的《理性与疯狂》作为武汉文革史专著，其研究方法和视野都有独到之处。<br />
杨小凯的《牛鬼蛇神录》和陈益南的《青春无痕》既是个人文革史，也是长沙文革史。杨小凯亲历了1966――1968年长沙的各个重大事件，还是“省无联”的骨干成员，写了《中国向何处去》大字报而被中央文革小组成员点名批判。《牛鬼蛇神录》记录了其在湖南多个看守所、监狱和劳改农场所遇到的形形色色的人物和个人的思想历程。 陈益南的《青春无痕》则是一个普通工人在文革中的造反经历记载，对长沙文革的重大事件都有个人的细致观察。 丁学良的一些回忆文章对安徽文革史有诸多记载和描述。 <br />
近些年来，由于网络的广泛运用，很多人的回忆得以在网络空间留存供后人传阅。<br />
2007年6月，原中共中央委员、兰州军区第一政委、甘肃省革命委员会主任冼恒汉中将生前口述写成的回忆录《风雨八十载》，由其女儿冼玲放上博客公开发表。这是文革时期担任过大军区和省级革命委员会一把手的高级干部中第一部全景再现式的回忆录。书中记叙了作者所了解的甘肃地区文化大革命全过程，对作者亲历或参与过处理的一些重大事件特别是有争议的事件（如青海“赵永夫事件”，宁夏“青铜峡事件”等）作了详细记叙。 <br />
继周孜仁之后，重庆另一位曾任《八&#8226;一五战报》主编的黄肇炎也写出了回忆录《沧海一滴》，其中第九章为《文革亲历》，发表于个人博客。原“重大八&#8226;一五战团”成员、校广播站工作人员张德昂，先后写出了《文革杂忆》、《文革岁月》，在其博客上连载。此外，原“重大赤卫军”成员黄昌国的回忆录《文革中我在重庆当保皇派和炮打中央文革的回忆》，已经在网络上流传。原重庆“反到底”负责人、重庆市革命委员会副主任李木森已写成《亲历重庆大武斗――重庆反到底派一号勤务员自述》（何蜀整理），王年一、余汝信分别为其作序。 <br />
湖南衡阳退休老干部应山红主编《雁阵惊寒：湖南衡阳文化大革命史事》一书汇集了多人撰写的衡阳文革史文章。《荣辱浮沉南柯梦》一文详尽介绍了文革时期衡阳群众组织的名称、派别和演变发展情况。《十年天地干戈满，四海苍生痛哭深》一文历数了发生在衡阳的11次大小武斗。《火烧后院，祸起萧墙》一文记载了衡阳地、市党政机关在文革中的情况，尤其难得的是忠实记录了这些机关中的中下层干部中造反活动。该书由应山红先生自费印刷并广赠国内外大学和研究机构图书馆，在香港中文大学中国研究服务中心图书馆、复旦大学、南京大学以及美国部分大学均纳入馆藏。邵阳退休老干部陆芒的回忆录《老虎坪纪事》和张介山的《非常岁月》均记载了邵阳地区文革的重大事件和不同人的遭遇。两书忠实记载了邵阳地区抓“\杀队”、“刮红色台风”、“清理阶级队伍”、“一打三反”等政治运动，反映了文革对基层和农村干部、群众的极大伤害。尤其是对抓“\杀队”运动的记载，为了解“道县事件”的影响和危害具有重要的补充意义。<br />
第六，其他主题。<br />
不少学者在其他方面的研究都有很多成果，如知识青年上山下乡运动、教育革命、文革时期国家制度和机构、文革文艺等等。<br />
随着上世纪90年代以后知青研究的升温，大批知青研究著作和文章出版，较具有代表性的是刘小萌《中国知青史：大潮》、 《中国知青口述史》， 杜鸿林的《风潮荡落：中国知识青年上山下乡运动史》，托马斯&#8226;伯恩斯坦的《上山下乡》， 刘小萌、定宜庄等人主编的《中国知青事典》，顾洪章主编的《中国知识青年上山下乡始末》和《中国知识青年上山下乡大事记》，潘鸣啸的《失落的一代：中国的上山下乡运动》 等。研究文革前知青的有定宜庄的《中国知青史：初澜》， 邓鹏的《无声的群落》 和《无声的群落(续集)》 等著作。其他大多是回忆性的文章，散见于各地文史资料和知青回忆录。</div>

							
							
							
							
