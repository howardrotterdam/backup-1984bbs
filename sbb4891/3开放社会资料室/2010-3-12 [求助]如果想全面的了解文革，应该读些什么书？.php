																											<div id="postmessage_554179" class="t_msgfont">2 专题类研究<br />
最先将文革作为研究对象的无疑是国外学者。文革爆发后不久，《中国季刊》（China Quarterly）、《亚洲概览》（Asian Survey）、《共产主义问题》（Problems of Communism）等学术刊物就有关于文革的文章发表。之后，随着文革的影响越来越大，更多的学者对此倾注了研究的热情。麦克法夸尔、费正清、沈迈克等成为文革研究的佼佼者。国内的文革研究起源于80年代。 以高皋、王年一、金春明等人的通史类著作为代表。直到今天，学者谈起文革，仍不免有“文革在中国，文革学在外国”的慨叹。<br />
文革研究中一直存在两种视角的分野，一是关注上层政治精英，一是侧重普通群众的研究。西方学者最初的研究，材料来源主要是红卫兵的大字报和出版的各种小报，研究的兴奋点在于权力斗争――高层政治精英的清洗和升迁。 对政治精英博弈的研究一开始就成为了文革研究的主流。伯雷汉（Philip Bridgham）在《中国季刊》上的系列文章均是对毛的关注，如《毛的文革：起源和发展》（Mao’s Cultural Revolution: Origin and Development）、《1967年毛的文革：夺权斗争》（Mao’s Cultural Revolution in 1967: The Struggle to Seize Power）、《毛的文革：巩固权力的斗争》(Mao’s Cultural Revolution: The Struggle to Consolidate Power)等。 黄靖从党内派系角度分析政策变动。他认为建政之前，党内派系在各自孤立的区域发展，使得各山头与下属拥有密切的关系网络；在一党专政的政治体制下，这些既存的关系网络转化成派系。基于此，他把党内派系作为自变量，解释派系如何影响领导人之间的关系和决策过程。 <br />
研究要重视普通群众的观点之所以盛行，与西方中国学研究的变化密切相关。60――70 年代的美国中国学界，社会史、地方史开始变得越来越重要，法国的年鉴派、社会科学派对美国史学界的影响越来越大，“上层人物”、“精英人物”、“官方史学”不再是史学关注的焦点，普通民众、社会底层开始越来越受到历史学家关注。 反映在文革研究领域，很多学者放弃宏观的叙事结构，从个案入手，对某地区或单位的文革进行了观察和剖析，取得了丰硕成果。 70年代末和80年代初，注意力转向红卫兵的派性斗争、社会矛盾和冲突。除进一步收集文革期间各种出版物资料外，学者们对离开大陆的中国人做了大量采访积累了可靠、可贵的资料。遵循关注普通民众、社会底层和分析中国社会内部矛盾的思路，形成了解释文革的“社会冲突论”。 <br />
最早分析群众分裂对立原因的是韩裔美籍学者李鸿永（Hong Yong Lee）。他在1975年的一篇文章指出，文革期间放手发动群众的过程中，某些社会集团要求激烈地改变中国政治体制，而另一些人则力图维持现状；二者的基本区别是对运动的看法不同，一派攻击“走资派”，一派攻击知识分子和“牛鬼蛇神”；家庭出身问题成为突出的矛盾。 他指出，激进的群众组织主要由无特权的社会集团组成，而保守分子主要来自优裕的社会集团。 <br />
陈佩华、罗森、安德佳对原广州的红卫兵作了长时间的采访，发表文章对李鸿永的观点提出修正，揭示了学生红卫兵派性之后的社会冲突。《学生和阶级之战：广州红卫兵冲突的社会根源》（Students and Class Warfare: The Social Roots of the Red Guard Conflict in Guangzhou&lt;canton&gt;）一文认为学生的冲突来源于几个方面：第一是60年代中期大部分城市学生向上的前途并不宽阔；第二是学生升学的压力增大导致学生竞争加剧；第三是竞争孕育了不同家庭背景学生之间的矛盾。最后，阶级背景的对立在文革中转化成红卫兵的派性。 陈佩华、罗森、安德佳三位后来分表发表专著，对上述观点作了更详尽的阐述和发挥。<br />
对群众的重视、视角向下的思路得到越来越多的重视，“社会冲突论”也得到学界的重视，对于群众的专题研究成果日益增多。<br />
第一，红卫兵运动研究。<br />
对文革中群众组织的研究最初几乎全部集中在对红卫兵的研究。因为文革的蔓延与红卫兵具有直接关系，所以红卫兵一直是专题研究中的重要内容。 前文提到的李鸿永等人，研究的出发点就是红卫兵运动。陈佩华、罗森、安德佳的《学生和阶级之战：广州红卫兵冲突的社会根源》（Students and Class Warfare: The Social Roots of the Red Guard Conflict in Guangzhou&lt;canton&gt;）一文是红卫兵运动研究的高水平文章。此后，西方学者对红卫兵运动的兴趣长盛不衰。<br />
大陆学界对红卫兵的研究开始于80年代，比较有影响的论著有朱世雄《从红卫兵运动的历史悲剧看否定文化大革命的必要性》（《青运史研究》，1985年第2期）、陆建华《红卫兵：一个社会结构分析》（《当代青年研究》，1988年第1、2期）、刘青峰《民族主义和超意识的复苏――略论红卫兵的精神素质》（《当代青年研究》，1989年第1期）等。这些论著具有拓荒性的意义，但是绝大多数著述都侧重于抽象的理论论述和定性分析，而疏于相关史料的发掘运用，观点和结论显得笼统空泛。有的论著虽然有一些简单的论证，但是这种论证要么完全基于论述这个人的狭隘经验和主观感受，要么完全基于当时报刊资料和领导人讲话的字面解读，在解释红卫兵运动的成因和派性斗争问题时带有简单化和以偏概全的倾向。 <br />
90年代以后，大陆学界关于红卫兵运动的研究有了长足的进步，学者研究的问题进一步细化和深入。印红标《红卫兵运动的主要流派》一文大致理清了红卫兵的派别和差异。他认为在红卫兵运动的派别分歧中，具有政治和社会意义的主要流派有四个：老红卫兵、保守派、造反派和极左派。老红卫兵是率先打破常规的干部子女“阶级队伍”；保守派红卫兵是依靠党组织的学生“积极分子”；造反派红卫兵是最高领袖指挥的平民子女造反者；极左派是基本政治和社会制度的批判者。他们在斗争方向和社会背景方面存在明显的差异，因而显现出各自的政治特点和行为倾向。在四个主要政治流派中，老红卫兵和造反派先后充当了红卫兵运动的主导流派。 印的文章其实只是北京红卫兵的划分，所以突出强调了老红卫兵，并认为老红卫兵和造反派先后成为运动中的主要派别。在北京之外，很多省市的情况是没有单纯的老红卫兵组织，高干子弟都在所谓的保守派红卫兵组织中，亦即在北京之外的很多省市，保守派和造反派才是红卫兵组织的主要派别。所以，徐友渔在《“文革”中红卫兵的派别斗争》和《形形色色的造反》等文章和著作中，均认为红卫兵的派别对立首先有保守派和造反派的斗争，保守派失败后，造反派内温和派和激进派严重对立，大武斗发生在造反派内部，派性斗争长久不能解决。 <br />
唐少杰在《从清华大学“文革”个案谈红卫兵运动的失败、情结及评价问题》文章中考察了清华大学红卫兵的产生、演变和派别。 叶青充分运用档案文献对福建的红卫兵组织名称、派别、组织特征及演变过程有深入细致的分析。 其他关于红卫兵的著作还有唐少杰的《一叶知秋：清华大学1968年百日大武斗》， 陈焕仁的《红卫兵日记》， 鲁礼安的《仰天长啸：一个单监十一年红卫兵狱中吁天录》、 周孜仁的《红卫兵小报主编自述：中国文革四十年祭》， 宋柏林的《红卫兵兴衰录：清华附中老红卫兵手记》。 <br />
此外，由于大陆与海外学界的交流增加，一些新的观念、视角和方法被引入，以往那种带有过于宏观和抽象的思维方式和叙事结构开始改进，更多的史料得以采集、发掘和运用。徐友渔通过对文献、口述史料的收集整理，在《红卫兵行为动因的调查和分析》一文中对红卫兵运动的产生和红卫兵行为的原因作出阐释。他分别考察了文革前的教育和社会文化环境对青年学生成长和性格养成的影响，提出了革命至上、反文化和反文明倾向、斗争哲学、阶级路线、意识形态的限度五个方面的因素。他们投身运动的普遍性因素是青春期激情，在此基础上，老红卫兵对阶级斗争的狂热宣传和推行、党的两种声音、文革发动者的策略手段以及社会矛盾和政治矛盾都是重要原因。 这些观点在《形形色色的造反》一书中有更加全面和详尽的表述。 同时，该书吸收了陈佩华等“社会冲突论”的成份，对普通群众和社会矛盾均有深入探讨。董国强认为，在新一代学院派研究人士的思想意识中，关于群众运动的研究已经不是文革研究的侧翼或点缀，而是主攻方向之一。</div>

							
							
							
							
