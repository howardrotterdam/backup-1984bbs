													<h2>狗屁话</h2>
						
						
																											<div id="postmessage_110880" class="t_msgfont">我始终坚信，在大多数情况下，对历史，我们一无所知。马克思主义哲学说得好，历史是现实与逻辑的统一――因此，无论我们如何歪曲，如何纂改，如何粉饰――都是合理的。生在新中国，长在红旗下。我们骄傲地套上崭新的缰绳，穿上崭新的蹄铁，把自己想象成花骨朵――于祖国的花园中含苞待放。阳光明媚，春风拂面，树影婆娑，大自然所有的美好都为我们而造。沉醉，沉醉啊，不他妈心旷神怡简直是辱没祖宗。 <br />
<br />
 <br />
&nbsp; &nbsp; 无论高智晟，刘沙沙还是胡佳，真的勇士不怕死在敌人的刀下，却怕淹死在自己人的唾液中。我们永远都是这样――枪打出头鸟，我再拽上几根羽毛，吐上一口浓痰。20年前的事情到今天大概也是这个状况。要么一无所知，要么闭口不言，要么“活该”，要么“成事不足败事有余”，再要么“当时已没有其他办法了，不然国家会乱下去”。真相？――没有，我们从来没有真相，我们甚至深信自己不需要真相。极权主义的余毒莫名其妙地在我们身上又肆虐了20年。最后得出结论：真相，真相就是外国也一样！哪有当官不榨民，哪有警察不虐囚，哪有奶粉不参毒，哪有地震不死人！我们简直狡猾得让当局都一愣，继而轻蔑一笑：屁民就是屁民。<br />
<br />
 <br />
&nbsp; &nbsp; 20年前，我两岁。那时候的年轻人尽管物质匮乏，却有个健全的大脑。不像我们在口水中泡胀的豆腐渣――只有热爱。那时候，是49年后中国最好的时代，是苦难中走出的弱视人的小心试探，是对所有美好的渴望。那时候，我们还敢于发出自己的声音，让灰色细胞中迸发的活力跳跃在公众视野，响彻在所有耳膜之外。那时候，是惊醒，是觉悟，是愤怒，是要求改变。然后就是沉默。继续沉默。现在的年轻人，也分正义与邪恶。正义属于抵制家乐福的热血青年，属于保护闹运圣火的年轻女孩，属于呼吁对藏民采取强硬态度的优秀汉民，属于想到用上厕所时间就能武力控制台湾就忍不住开怀大笑的爱国军事迷。邪恶属于90后，属于非主流，属于哈韩一族，属于为20元游戏币就跟网友搞一搞的女游戏玩家。而那些有棱有角的年轻人呢，继承54、64精神的年轻人呢，呼吁民主自由的年轻人呢？――他们是，汉奸！卖国贼！别有用心！他们远在正义与邪恶之外，他们是狗！哈哈。我们这个时代的逻辑，精妙过任何一部机器；我们的朋友们眼睛雪亮――决不揉进一粒沙子。<br />
<br />
 <br />
&nbsp; &nbsp; 说了这么多废话，今天的主题其实是音乐。崔盟主有句话说得好：音乐只有好坏之分。摇滚乐――我不会再提，挂羊头卖狗肉的烂货，殊不知您这羊头也不好卖。说实话，让诸位见笑，港台音乐我了解实在不多。上初中那会儿还会哼两首，后来装逼――两耳只闻摇滚乐，所有港台的东西都盖上“垃圾”戳，扔一旁不屑一顾。事实上是，这其中的好音乐比比皆是。关于音乐的意义，我认为有三种：现实性，实验性与娱乐性（尽管我不喜欢）。至于音乐现实性中的政治性，某些仁兄神色销魂地说：什么东西牵扯到政治就不再纯洁了（类似说法很多，比如：牵扯到政治的艺术就不再是艺术）。而据我所知，某些音乐的意义在音乐之外。就政治本身而言，也并非天下乌鸦一般黑，政治是可以很美好的。如同你穿衣吃饭一般，难保某一天买件坏衣服，拌饭里卧了半只苍蝇。即便政治本身并非美好，音乐的政治性不在歌颂，而在鞭挞――鞭挞了丑恶的政治，何来狗屁纯洁不纯洁。政治权利是公民的基本权利，它应该像穿衣吃饭一样稀松平常，而不是束之高阁，视为禁忌。而某些音乐，作为表达政治权利的途径，再正当不过了。和绘画等其他艺术形式一样，我简直找不到比这更好的表达途径啦！委婉，深刻，形象，易于传播，影响力大――妈的，神啦！艺术不是什么阳春白雪，而是根植于民间。<br />
