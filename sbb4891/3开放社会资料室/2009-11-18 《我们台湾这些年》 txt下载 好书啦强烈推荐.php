													<h2>带三个表: 关于台湾</h2>
						
						
																											<div id="postmessage_352542" class="t_msgfont"><a href="http://www.wangxiaofeng.net/?p=4313" target="_blank">http://www.wangxiaofeng.net/?p=4313</a><br />
“台湾”这个词对海峡这边的人意味着什么是不言而喻的。一般都是这样，第一，因为政治上的原因，我们不得不知道台湾；第二，因为大众文化，我们不得不知道台湾。但这两种“知道”，这么多年并没有让我们知道真正的台湾是什么，虽然用的语言一样，可恰恰是这两方面的“知道”把台湾给妖魔化了。<br />
<br />
去年，有个朋友去了一趟台湾，回来后我问：台湾什么样？对方说：没劲，台北太小了，跟开封差不多。<br />
<br />
靠，开封能出来周杰伦或者邓丽君吗？我心想，这趟丫是白去了。所以我就有一个计划，最近打算去一趟台湾，好好看看那里，是不是跟开封一样。一个台湾朋友说：你来吧，我带你看台湾最美的地方，别人都是看不到的。我说，你等我把那台游戏电脑卖掉，买一台好相机，马上就去。<br />
<br />
我认识的第一个台湾人是滚石唱片公司的，我的感觉是，她好像一直觉得大陆还停留在大炼钢铁的年代，我当时对她的这种看法感到很不舒服，但时隔多年，我觉得她的判断确实有道理。外表上我们都很现代了，内心深处不是仍然在大炼钢铁吗――我们恨不得在物质上一夜超英赶美。<br />
<br />
后来遇到一个从事IT行业的台湾人，他面试我，我发现，他说的话我都能听明白，但使用的词我要愣一下，比如“记忆体”“列印机”“网际网络”“软体”……那次面试让我觉得很好玩，长得都差不多，交流上已经有明显的差异了，这种差异的背后是什么呢？是五十多年的隔阂，其实两岸之间都相互太不了解了。这些年我们对台湾的了解就是明星而已。而且，往往台湾的东西是经过一番符号化的方式被我们接受，即便是今天有了互联网，好像对台湾也没什么了解，因为那堵高墙比那道深深的海峡还难逾越。<br />
<br />
有一个70后的台湾人，叫廖信忠，在大陆生活，写了一本书：《我们台湾这些年》。因为写这本书，他还丢了工作。这本书讲述了70年代至今台湾的变化。客观地讲，作者的文笔不像很多作家那样优美，但是他却写出了文笔优美的作家没有写出来的一种东西：把最细节和最朴实的东西告诉了我们，那种符号化的东西没有了，把台湾还原回最生活的层面上，有血有肉，这更有助于我们了解台湾。更何况，这本书就是针对不了解台湾的大陆人写的。看这本书才发现，以前我对台湾的了解，有很多偏失的地方。<br />
<br />
台湾人来大陆后会觉得很牛逼，瞧不起大陆人，为什么会这样？就是因为在90年代台湾经济突然腾飞，没办法，硬实力就是硬实力，当时台湾的外汇储备列全球第二。尤其是，最先与大陆产生交流的正是台湾最强的文化娱乐，这就像田忌赛马，至今台湾人仍觉得他们在娱乐这方面比大陆的乡镇企业强。<br />
<br />
但这只是台湾很小的一部分，只是被放大了而已。在《我们台湾这些年》中，作者告诉我们的是，台湾这些年活的也挺憋屈，首先在国际上的政治身份被取消了，同时也经历剧烈变化，也有很多搞不懂的事情，大家也很迷茫。尤其是，历史原因造成的那种挥之不去的乡愁，以及因此带来的一些社会问题。每看到这里，我就想，这他妈是谁造的孽啊。<br />
<br />
廖信忠老师的这本书，一方面是自己的成长史，一方面是台湾三十多年的历史，个人的成长穿梭于历史之中，看上去会更加真实。尤其是看到一些两边能共同感受到的故事，会会心一笑。廖老师比我小整整十岁，但是故事里讲述的有些东西，却是共通的。这种共通来自时间差，正好是大陆造孽的十年。<br />
<br />
有些段落，谈到台湾的政治状况，让我总有一种冲动，如果是电子文本，我会马上把“台湾”替换成“大陆”，读起来照样逻辑通顺。廖老师感叹：大家都不容易！某种程度上讲，两边的生存状态差不多。<br />
<br />
在这本书里，我读到了一句话，是蒋经国老师说的：世界上没有永远的执政党！</div>

							
							
							
							
