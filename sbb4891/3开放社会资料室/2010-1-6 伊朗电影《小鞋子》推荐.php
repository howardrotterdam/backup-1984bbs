													<h2>伊朗电影《小鞋子》推荐</h2>
						
						
																											<div id="postmessage_417601" class="t_msgfont">听过这部影片的另一个名字，朴实又简单－－《小鞋子》；看过这部影片的那张别致的海报， 湛蓝的天空，漂游的金鱼，手拉手的两个孩子的背影。让我想起了一个词：简约主义。对，简单的，脉脉的，但可能是直指人心的感动。再没有看之前，这部影片给我了这样的第一印象。<br />
<br />
之前很少接触伊朗的电影，换句话说习惯了好莱坞的光影斑驳的我，似乎对这种“草根文化”有点淡然了。但是，“天堂里的孩子们”拯救了我的默然，呼唤出了隐藏在我心底很长时间的那份性灵。<br />
<br />
一双已经破的不堪入目的粉红色的小鞋子，在一双粗糙的手中，一针一线的缝补着。开头的这一系列动作干净利落却给人以压迫的冗长。当鞋匠把缝好的鞋子递给那个叫阿里的小男孩后，我知道了原委：他替妹妹来补那双破旧的已经补过不知道多少次的小鞋子，原因，显而易见：贫困的家庭，生病的母亲，只有父亲的微薄的工资支撑着这个看似破败的家庭。当导演把这些情节都娓娓道来的时候，我的心头涌上了一丝担心，看过太多这样的情节，这样雷同的故事，并不是我缺乏同情。只是这样普通的故事，稍微脱轨便会落入俗套，陷入单纯赚人眼泪的表象漩涡，直到让人麻木。<br />
<br />
但是，接下来的发生的事情，让我完全打消了顾虑。不，应该说让我完全陷入了故事当中，没有一点闲暇去考虑别的事情。<br />
<br />
阿里在帮大人买菜的时候不小心弄丢了那双鞋。他妹妹唯一的鞋子，被捡垃圾的人“收入囊中”。他不敢告诉爸爸，开始我以为是害怕挨打，他也是这样告诫妹妹的:千万不能告诉爸爸，家里没钱给你买鞋的，他知道了你也会挨打的。到后来我才知道，阿里，一个11岁的学习优异的伊朗小男孩，不单单是害怕父亲大发雷霆，他害怕的是忧郁的母亲的眼里再添上一层困苦，他担心浮躁的父亲的头上再添一根白丝。他不想让本来就困难的家里因为他的原因再添上一丝负担。或许小小年纪的他想不到这么具体这么深远这么细腻，但当阿里看着妹妹嗔怪的表情急得已经哭出来的时候，从他天真的面庞和晶莹的泪珠里，我升华出了这一切，提炼出了小孩子的天真善良独特性灵的想法里蕴涵的最天然的特质。<br />
<br />
为了解决只有一只鞋的难题，兄妹俩悄悄达成了一个协议，每天妹妹穿着哥哥的旧球鞋去上课。等放学后，妹妹快速飞奔回家，哥哥换了鞋子再飞奔的去上学......<br />
于是，我看到了这样的一幕幕：兄妹俩在巷子里飞快的进行那熟悉的“交接”动作；阿里穿梭在狭窄的巷子里的瘦弱背影，气喘吁吁的在上课铃响后才无奈的踏进教室的失落神情，被校长抓到后委屈而又难过的哭泣；还有还有 ，妹妹追着不小心掉入河里随波游走的球鞋时的无助和急迫，她收到哥哥为了安慰她而送她的那支笔时欣喜而开朗的笑容。一个属于两个天真的孩子之间的许诺，串起来的是属于孩子们自己的苦乐酸甜，苦中作乐，酸涩品甜。<br />
<br />
<br />
导演独具匠心的设计了许多巧妙的特写镜头，他运用的镜头语言就好像一个孩子在观察外部世界那般细致新奇，让那种独特的视角真实可爱的令人击节。<br />
<br />
令我印象深刻的是妹妹在考试时的那段情节，已经下课了，她一边答着题一边偷偷望着老师手腕上的手表，这时的镜头没有给出老师的全身，而只有那种让人略感不安和焦躁的在手腕上移回，让我刹那间触到了妹妹同样焦急的心情：哥哥在等着她回去送鞋子呢，现在还又时间吗？<br />
<br />
另一个出彩的镜头同样是描述妹妹微妙的感情。站在队伍中间，低头扫视着周围人的鞋子。导演不厌其烦的用慢慢移动的特写镜头向我们展示了周围那些大大小小的或者玲珑或者华贵的漂亮的鞋子，最后，镜头回到了自己的脚下，那双又大又旧的破球鞋。几乎不用在回到妹妹的表情，我已经可以尝到她内心的羡慕和无奈失落了。几乎每个人，在童年时不都曾有过这样的时候吗，那份孩子的小小的不平衡的心理状态被敏感的我的记忆瞬间捕捉到了。但是真正高明的，是让我忆起这种感觉的，像个真正的孩子一样表达感情的的导演啊！<br />
<br />
<br />
开始的时候不明白为什么本片要叫作“天堂的孩子”。此前我曾看过这样一篇同样描述贫穷的文章，题目，却残酷让我不忍读出－－贫穷，是一根刺痛的针。文章描述的作者的世界是灰暗的，贫苦的环境和本片相似，但基调却几乎完全不同。<br />
<br />
直到看完影片，我才领会了天堂的含义，虽然出身贫苦，但兄妹们却依然会不时的爽朗的开怀的大笑，这是低微的物质生活不能夺走的，也是最珍贵的。他们在刷鞋子时还不忘吹起一个个五光十色的肥皂泡泡，哥哥在难得的闲暇时忘情的陪着雇主的小孩玩耍，在秋千上悠悠的晃荡，花园里在爸爸的浇灌下分外红艳的花朵，把骄阳烈日也变的柔和而温馨。真正生活在天堂的孩子们，也不过如此吧。能够如此从容乐观的面对生活的人们，肯定拥有天堂了吧！<br />
<br />
从城里打完工后，父亲带着阿里载着胜利的欢愉缓缓的走在林阴道上，注意到他们的对话了吗？<br />
<br />
“这份工作不错，比以前的工作能赚到钱，以后每个星期日我们都出来干。过不了多久，我们就有钱了。有钱以后，我给你们买很多东西。”<br />
<br />
“先给妹妹买双鞋，她的鞋早就破了!”<br />
<br />
“你们想要什么就买什么!”<br />
<br />
一直以来眉头紧缩的父亲这时候也像个孩子般欣喜。<br />
<br />
不知怎么的，这个普通的对话让我想起了《活着》。<br />
<br />
福贵背着儿子有庆走在上学的路上，有庆稚气十足的问爸爸：“爸爸，小鸡长大了是怎么样的呢？”<br />
<br />
“小鸡长大了就变成了鹅；鹅长大了，就变成了羊；羊再长大了，就变成了牛；等牛长大了，馒头也长大了，共产主义就到了。”<br />
<br />
同样是浓浓的父子情，同样是温情的话语，同样充满希望的憧憬却在下一刻同样的戛然而止。<br />
<br />
父亲带着阿里的自行车失去控制撞到了树上。坐在旧货车的后舱里头上缠着绷带的父亲无力而迷茫的眼神让人目不忍视。福贵的儿子就在和爸爸分开后的那一个下午，被倒塌的墙壁压死了。福贵听到噩耗后那疯狂的歇斯底里的痛苦表情，是我看过最残酷的画面。<br />
<br />
<br />
毕竟，生活是现实的。<br />
<br />
但现实，也不是灰色的同义词。哥哥报名参加了5公里的长跑比赛，因为第三名的奖品，是让他望眼欲穿的一双鞋子。当他信誓旦旦的向妹妹保证一定会拿到第三名的时候，他们脸上再次绽放了纯真的笑容，那里面包含的是希望，是信任，是孩子们的固执坚持。<br />
<br />
比赛的过程,没有任何的声音，除了孩子们沉重的喘息。每一次都让我的心头一悸，镜头跟随着阿里，他瘦弱的身躯他满头的汗滴他艰难的喘息，最重要的，是他心中的希望!<br />
<br />
这个镜头如此漫长，好像有一万光年！<br />
<br />
每次落后时，镜头开始闪回，他脑海里浮现的是妹妹无辜的眼神，自己每天跑步的辛劳，是他和妹妹订立的那个金子般珍贵的诺言。让他超越身体极限，拼了性命冲刺的仅仅是一双鞋啊！不，那里面凝聚的是妹妹的心。<br />
<br />
看到这里我觉得心里堵的想用眼泪来释放，曾几何时我以为自己已经遗忘了这样的感觉，不是因为什么所谓的精神什么所谓的浮夸的感动。仅仅是他那份纯真的坚持，就为了一个目的，就是为了心中的希望，不管怎么样就算累趴下也要坚持的向它靠近。这份坚持，难道只有在孩提时才那么强烈那么无畏那么不可抗拒吗？慢慢长大的我们却在长大中迷失了我们最应该坚持的东西！心痛的不能自已！<br />
<br />
阿里用最后的力气第一个冲过了终点线。他的父亲，老师，记者，当所有的人都把他捧着欢呼雀跃的时候，阿里却气喘吁吁的问道：“是第三名吗？”<br />
<br />
站在领奖台上，举着冠军的奖杯，阿里满脸的泪水，人人都以为那是胜利的激动。阿里却在表情黯淡的抽泣，抽泣的悲伤是因为他心中希望破灭，是因为违背了和妹妹的诺言啊！<br />
<br />
<br />
还好，我看到的是一个温暖的故事;还好，他们认为自己生活在天堂里。当父亲从商店里出来拿着两双鞋子的时候，我感到心里前所未有的满足。也许人心真的本善，即使有多残酷的经历，还是希望心中的美好实现，毕竟，这个世界的永恒不多。<br />
<br />
还为自己的失误耿耿于怀的阿里脱下那双被他磨破的球鞋，把被磨烂的脚伸到了池塘里泡着。<br />
<br />
这是影片最后的神来之笔，镜头再次变成了特写，而这次是在柔和的晕蓝的池水里一双小脚，柔和的音乐中，一抹亮色突然映入眼帘。一群金黄的金鱼游到了周围，在徘徊，在嬉戏，在灵动的摇摆着精致的身躯。<br />
<br />
其实，生活的美好本不抽象，就像那忧郁的蓝色中突然跃入的一抹亮色，让人惊喜，让人久久不能平静，让人浮想联翩......</div>

							
							
							
							
