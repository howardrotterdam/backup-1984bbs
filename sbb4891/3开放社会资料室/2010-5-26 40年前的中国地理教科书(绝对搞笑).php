　　40年前的中国地理教科书(绝对搞笑)<br />
　　阿尔巴尼亚：阿尔巴尼亚人民共和国在巴尔干半岛的西南部，整个领土与其他社会主义国家不相连，处在资本主义国家的包围之中。阿尔巴尼亚人民同美帝国主义和现代修正主义的代表铁托集团进行着不屈的斗争。阿尔巴尼亚人民大规模开垦耕地，兴修水利，耕地面积比解放前扩大一半以上。首都地拉那位于中部，解放前，这里工业很落后，解放后，兴建了各种工厂，并修建了通往港口都拉斯的铁路。<br />
　　　　<br />
　　　　南斯拉夫：目前，统治这个国家的是工人阶级的叛徒、现代修正主义的代表DD铁托集团。南斯拉夫依赖着美帝国主义和其他帝国主义所谓“援助”，工业原料、小麦甚至食用油等生活日用品也要靠美国、西德、意大利等国供给，南斯拉夫的农业相当落后，绝大部分是个体经济，私人占有土地，可以雇佣别人耕地，进行剥削，农业除个别年份，都未能达到战前（1938年）水平。<br />
　　　　<br />
　　　　意大利：意大利是帝国主义国家，在非洲有许多殖民地。意大利的耕地大部分掌握在地主和资本家手中，农业生产一直处于落后状态。波河平原上有资本家的农场，南部是落后的农业区。意大利人民的生活非常贫困，失业人数居资本主义国家首位，每年有大批人到国外谋生。意大利首都罗马在半岛西海岸的中部，是历史上有名的古城，这里的工业非常落后，反动统治者害怕工业无产阶级聚集在京城里，不敢在这里建设大工厂。<br />
　　　　<br />
　　　　德意志民主共和国：二战前，德国是一个凶恶的帝国主义国家，1945年德国投降后，苏、美、英、法四国根据协议，分区占领德国，以便彻底根除法西斯势力，帮助发展民主力量，建立统一的、爱好和平的、民主的德国。当时规定苏联占领区在东部，美、英、法占领区在西部。苏联执行了协议，在东部肃清了法西斯残余，开展民主运动。而美、英、法帝国主义违反协议在德国西部扶持军国主义势力在49年9月建立了垄断资本集团和大地主统治的德意志联邦共和国，制造了德国分裂。在这种情况下，德国人民于同年10月在东部建立了德国历史上第一个由劳动人民当家做主的德意志民主共和国。战前，德国东部工业基础很薄弱，加上战争中美、英和德国法西斯的破坏，解放初期的发展遭到很大困难，由于劳动人民的积极努力，十多年来，经济不仅获得恢复，而且发展非常迅速，现在已经成为欧洲最主要的工业国之一。（该课还配有插图：柏林的斯大林大街美景。）<br />
　　　　<br />
　　　　德意志联邦共和国：西德是帝国主义国家，在美、英、法等帝国主义国家的扶持下，西德的军国主义已经复活，庞大的正规军正在加紧重建，并在国外建立许多军事基地。它不仅日益严重地威胁欧洲的安全，而且危害着全世界的和平，已经成为严重战争危险的策源地。西德的农业比工业落后许多，粮食不能自给，只有畜牧业比较发达。<br />
　　　　<br />
　　　　英国：英国是最老牌的帝国主义国家，到19世纪中叶，它在全世界占有比本土大150倍以上的殖民地。但随着民族解放运动的蓬勃发展，许多殖民地都取得了独立，它的殖民体系正在崩溃。英国工业生产的绝大部分被少数资本家集团所垄断，工业相当落后，设备陈旧，生产成本高。英国工业发展缓慢而且极不稳定，其中民用工业长期停滞甚至显著下降。英国工农业发展极不平衡，农业绝大多数被大地主所控制，伦敦平原是主要种植区。粮食生产只够本国需要的一般，大部分田地种植牧草和饲料作物。<br />
　　　　<br />
　　　　法国：法国和英国一样，也是一个老牌帝国主义国家。二战前，它在全世界占有比本土大20倍以上的殖民地。但随着民族解放运动的蓬勃发展，许多殖民地都取得了独立，它的殖民体系正在崩溃。（我惊讶的发现这段居然和描述英国的一模一样！）法国工业生产集中在少数垄断资本家手中，法国资产阶级长期以来把大量资本输往国外放高利贷，影响了本国发展。所以法国工业生产远远落后于美国、英国、西德等国家。农业在法国经济中占有重要地位，但农业发展很不平衡，北部地区是使用机器耕种的资本主义大农场，其余地区是落后的小弄经济，而中央高原的畜牧业更处于落后状态。<br />
　　　　<br />
　　　　南非：（特意说一下南非的种族隔离制度，可以拿现在的户口制度做个对比）南非联邦政府先后颁布了“通行证法”“种族隔离法”“奴役法”等70多种种族歧视和压迫的法令，每一个法令就是一道枷锁，非洲人的权利和自由被剥夺干净。拿“通行证法”来说，这项法令规定年满16岁的非洲人必须携带“身份证”“迁移证”“寻职证”“纳税收据”“居住证”等20多种，以备警察检查。如果证件不全或者忘记携带，就要作为“无业游民”遭到逮捕。根据南非联邦警察局的报告，仅在1958年，就有将近60万的非洲人由于违反“通行证法”和“检查法”而被逮捕。（哈哈，真是太像了！）<br />
　　　　<br />
　　　　美国：美国是世界上最凶恶的帝国主义国家。密西西比河是北美洲第一大河，在密西西比平原上，由于滥伐森林和胡乱开垦草原，水土流失非常严重，密西西比河终年携带大量泥沙，造成河床增高，常常泛滥成灾。美国矿产、水力和森林资源比较丰富，但垄断资本集团为了追求利润，对矿产资源进行掠夺性的开采，他们只拣投资最小，利润最大的矿床开采，一旦感到利润小就将矿井废弃。美国本来有茂密的森林，但由于资本家的滥砍乱伐，森林面积减少一半。美国工业分布非常畸形，有3/4的工业生产及9/10的机器制造工业和钢铁工业集中在东北部的大西洋沿岸和五大湖地区。美国的钢铁工业是为军事工业服务的，产量极不稳定。美国南部是一个落后的农业区，土地大多集中在资本家手中，耕作技术相当落后，仅在棉花产区建立了一点纺织点<br />
　　　　<br />
　　　　如果那时候我学了这个，我一定认为美国、英国、法国什么的是全世界最落后的国家，“人民的生活非常贫困”，而且他们国家的人都愚蠢到极点，肆意破坏环境，破坏生产，甚至“害怕工业无产阶级聚集在京城里，不敢在罗马建设大工厂”！而所有的社会主义国家都是美丽富饶的，人民生活幸福，富裕，工业农业都得到极大发展！</div>

							
							
							
							
