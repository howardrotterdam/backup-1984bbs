																											<div id="postmessage_774351" class="t_msgfont">二：搜词的要领<br />
<br />
首先要明确当前阶段主要打击的对象，按照打击的方向去搜词。<br />
其中有一些固定的搜索方向：89事件、法轮功、反动言论<br />
另外一些阶段性的搜索，根据时间不同进行重点搜索：反日、中考等等<br />
搜词的宗旨在于精，不在于多。<br />
例如：<br />
搜索共产党，可以带出操共产党、打倒共产党等等<br />
搜索中央，可以带出打倒中央、推翻中央等等<br />
这样的词虽然搜索结果多，但是前后加的一些字简单明了就能看到，如果自己去想打倒共产党这样的词，难免有遗漏，要合理的利用网友的头脑。<br />
也有一些例外的情况，例如搜索党应该出现共产党，实际上搜索结果里共产党出现的并不全，这个需要大家在工作中积累经验。<br />
新发的帖子需要5分种左右才可以进入后台，搜词的时候会有这段延迟搜索不到结果，这个需要注意一下，不是搜不到东西就一定没有。<br />
另外搜词过程中，首先看讨论区，出现在敏感讨论区的应该更加重视，另外通过搜词，可以看到一些不好的讨论区，这时候应该发给贴吧巡视的人员。<br />
<br />
任何工作都是需要合理的搭配，搜词也需要和后台审核结合工作，例如 打 倒 共 产 党 ，这个内容在后台审核就很容易被发现，后台审核隐蔽性高的，搜词和前台巡视可能就容易发现了，贴吧是一个整体的团队，脱离了组织，任何一个独立的工作都是不可能完美的，大家一起努力才能使贴吧更好的成长。</div>

							
							
							
							
