初冬时节，我在北京……<br />
2006-12-31&nbsp; &nbsp; 母碧芳<br />
　<br />
一一记 读七届全国作代会&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp; 盛会闭幕,载誉归来，鱼鲠在喉，不吐不快，总觉有话要说，也该说说。因这荣誉属父老乡亲，属全川同仁，当以汇报，当以共享。夜深难寐，点击大脑，调出内存，记读点滴，以作叩谢！ <br />
 <br />
一、关于盛况<br />
 <br />
&nbsp; &nbsp; 中国文学艺术界联合会第八次全国代表大会、中国作家协会第七次全国代表大会于１１月１０日至１４日在北京举行。第八次文代会有４７个代表团、约１５００名代表和港澳台嘉宾出席。第七次作代会有３８个代表团、约９５３名代表与会，并特邀港澳台及海外华侨华人作家嘉宾出席。２５００多名文学艺术界代表及嘉宾齐聚一堂，共商我国文学艺术事业繁荣发展大计。<br />
&nbsp; &nbsp; 这次会议从中央到地方都很重视。各地以省代表团为单位，临行前，好些省都召开了欢送会，省级领导亲自饯行。我们省的省委常委、宣传部长王少雄因为在外地出差，特地写了一篇欢送词，请常务副部长候雄飞带到北京,在当天的预备会上给我们展读。（回来的时侯王少雄部长又代表省委、省政府给两代会代表接风，在成都新华国际大酒店专门举行了欢迎仪式。）各地大都是省委宣传部长或副部长作团长和副团长。级别最高的是云南和青海省，云南的团长是省委副书记丹增，青海的团长是副省长吉狄马加。丹增是中国作协副主席，吉狄马加曾是中国作协书记处书记，今年六月去青海的。我们省文代会团长是省委宣传部常务副部长候雄飞, 副团长是省文联党组书记黄启国。作代会团长是马识途，副团长分别为省委宣传部朱丹枫和省作协党组书记吕汝伦。<br />
&nbsp; &nbsp; 我们的代表到北京一下机场，大会的工作人员就在机场迎候，向每个代表送上一束鲜花。到宾馆后房间里也布上了鲜花和水果。大会把作家代表大都安排在北京饭店，大会还以强势媒体进行宣传报导。海内外有上百家新闻单位到会采访，图文并茂，有的报纸还发表了社论。中央党政军领导除黄菊外，全都出席了开幕式和联欢晚会。胡锦涛总书记两次与我们见面，温家宝总理和我们有三次见面。大会开幕式上，全国工、青、妇、军领导还向两会代表致词祝贺！除此之外各宾馆的保卫工作也做得非常到位，基本上和全国人大、政协会议是对等的。进入宾馆、就餐、参加晚会都要凭代表证，进入人民大会堂除按代表上的照片证验明正身外，还要用仪器进行安全检查。有中央领导出席会议时，全是一级警备（另外据说，现在一般人民团体举行会议时基本上由分管领导参加、作报告，总书记和总理都不一定参加了。可见中央对作家、艺术家们厚爱有加！）<br />
 <br />
二、关于我们团<br />
 <br />
&nbsp; &nbsp; 我们四川作家代表团是一个比较大的团，除海选的17位正式代表外，还有几位作当然代表的老作家和上届的主席团委员、全委委员，共26位。基本兼顾了老、中、青、妇和创作、组织、评论、出版、翻译各个门类。我们去的当天下午，好几家媒体就来向马老就他个人及我们团的组成情况进行采访。马老对记者介绍说，四川的代表是通“海选”出来的，既有文坛前辈，也有前途大有希望的年轻作家。看得出来，马老对自己带着的这支展示“文学川军”实力的作家代表团是满意的。<br />
&nbsp; &nbsp; 实际上我们的“川军”也的确实力不薄。我们团有获茅盾文学奖《战争与人》、已年迈八十多岁的王火和《尘埃落定》的藏族作家阿来，有以电影《达吉和她的父亲》而出名的高缨，有以《草木篇》而著名的诗人流沙河，有茅盾文学奖评委、著名评论家、前《当代文坛》主编何开四，还有著名诗人、《星星》先后主编杨牧、张新泉，梁平，《四川文学》主编意西泽仁，有四川文艺出版社资深编辑兼作家林文旬、成都报业集团作家兼翻译家王爰飞、有以农村题材《山杠爷》而著名的南充作家李一清，纪实文学《中国知青梦》的作者邓贤，凉山文联主席彝族作家倮伍拉且，有为全省文学组织工作做出了很大贡献的省作协领导：吕汝伦、宋玉鹏、傅恒、王敦贤、曹纪祖，还有目前正热播的电视剧《暗算》的作者麦家，还有此次新当选的小说家贺享雍、马平、罗伟章等。尤其是马老和高缨老师已连续六次参加了全国作代会，非常难能可贵。<br />
&nbsp; &nbsp; 为表达自己的高兴之情，93岁的马老特赋七律一首，祝贺大会开幕：你来海角我天涯，盛会幸逢聚老家。旧树凋残悲逝友，新枝勃发喜奇葩。八言荣辱遵谆训，双百方针茁美花。老凤稚雏齐唱和，挥毫泼墨颂中华。马老、王火、高缨三位德高望重的老人每天都和我们一起参加大小会议，没有拉下一次。讨论时都积极认真地发言，他们大气不古、思维清晰、言之有物、有理，给我们起了很好的典范作用。<br />
 <br />
三、关于主题词<br />
 <br />
&nbsp; &nbsp; 一个时期有一个时期的主题词。社会如此，政冶如此，文学艺术当然亦如此。记得上届两会的主题词是“与时俱进”，“星火相传”。自上次会议提出与时俱进后，就一直延用至今。<br />
&nbsp; &nbsp; 这次两代会的主题词是什么呢？那就是构建和谐文化。这个和谐文化和和谐社会事业是一脉相承的。那么，它又是在一种什么背景下提出来的呢？温家宝总理在给我们作报告中特别作了注解。他说，这是那次他去给老作家季羡林过生日时，季老给他提到和谐社会和和谐文化关系时提到的两个关键词，他当时很受启迪，后来就将和谐社会写进了总书记的十六大报告，进而推之，又将和谐文化写进了这次两代会总书记的报告。<br />
&nbsp; &nbsp; 尽管深秋的北京天气渐冷，而来自全国各地的两会代表却感受到了浓浓的春意。开幕式上，胡锦涛总书记拱手出场、三方鞠躬。报告时精神饱满，声若宏钟， 他首先代表党中央、国务院，向大会召开表示热烈祝贺！向全体代表，并通过代表们向全国广大文艺工作者致以崇高的敬意和诚挚的问候！在报告中，他充分肯定了我国广大文艺工作者为推动我国社会发展进步、弘扬民族精神、满足人民群众的文化需求、促进人民全面发展付出了辛勤的劳动，做出了重要贡献。他说历史将永远记住同志们的杰出创造和奉献，党和人民感谢你们！他站在时代的高度指出，繁荣社会主义先进文化，建设和谐文化，为构建社会主义和谐社会作出贡献，是现阶段我国文化工作的主题。他号召一切有理想有抱负的文艺工作者,“要担当起时代赋予的使命,积极投身讴歌时代的文艺创造活动”;“要密切同人民群众的血肉联系,积极反映人民的心声”;“要大力发扬创新精神,积极开拓文艺的新天地”;“要做到德艺双馨,积极履行人类灵魂工程师的职责”。会场内爆发出一阵又一阵热烈的掌声！<br />
&nbsp; &nbsp; 温家宝总理在给我们作国内经济形势报告时对代表们也是非常谦和、敬重。在报告开头，他大概用了一刻钟的时间列举他所崇拜的古今中外的作家和他阅读过的作品。其中他引用了一句屈原《离骚》中的句子，让人深受感动和启迪。一般人也引用《离骚》，但大都引用的第一句“路漫漫兮，其修远兮，吾将上下而求索。”温总理引用的却是《离骚》中靠近中间的一句：“长呔息，以掩涕泣，哀民生，之多艰……”。他念这句诗的时候，下面清风哑静，没有一点声响，一双双眼睛里挂着惊叹，噙着热泪。他还举例说，有一次他托人请李瑛老师给他修改一首诗，李瑛问，他还认识我呀？他说我不但认识，还要讨教呢。说到这儿，他问李瑛老师来了没有？李瑛站了起来，总理马上也站了起来，深深地向李瑛鞠了一个躬。会场顿时报以热烈的掌声。<br />
&nbsp; &nbsp; 报告结束时，总理没有什么套话，仅一句非常平凡、朴实的“一个小时四十分钟，大家累了吧？”作为结语，令人热泪盈眶，回肠荡气。<br />
&nbsp; &nbsp; 聆听总书记和总理的报告，作家艺术家们不约而同地用“亲切”、“新颖”、“深刻”、“震撼”来畅谈各自的体会；“感动”、“鼓舞”、“鞭策”、“振奋”成了大家表达内心感受最集中的词汇。如何把党的关怀化作文艺创作的巨大动力，成了与会代表们热议的话题。<br />
&nbsp; &nbsp; 此外，国务委员唐家旋还给我们作了国际形势报告。主要也就是中国与美、日、韩的关系。这几种关系大家都知道，就不一一阐述。我们国家总的态度（主要是指对美国）是：在合作中斗争，在斗争中合作。<br />
 <br />
四、关于联欢<br />
 <br />
&nbsp; &nbsp; 十一月十三日晚上也是一个令我们振奋激动的夜晚。晚饭后我们乘车去人民大会堂。一上车高缨老师就问我，碧芳今晚如果要你出个节目，你演什么呀？我俏皮地说，我就跳个你《达吉和他的父亲》中的《快乐的罗索》吧。他不禁好笑，又道如果人家欢迎你再来一个呢？我说，那我就再跳一次《快乐的罗索》，引来车里人一阵哄笑。我知道这是笑我傻。因为这样的联欢晚会节目早安排好的，根本不容随便上去表演，但我傻得有点可爱。<br />
&nbsp; &nbsp; 八点钟胡总书记和温总理以及其他国家党和国家领导人准时走进人民大会堂二楼金碧辉煌的宴会厅，又一次和大见面了。那一刻，两千多名代表一下都从座位上站起来了，掌声久久不息……联欢晚会上以“和谐阳光百花芬芳”为主题，不少艺术家代表献技献艺，表演了很多精湛的节目，我们四川泸州雨坛彩龙艺术团还出了个龙狮飞舞的节目。演出分新人新声、配乐朗诵、杂技、交响音画、作品展示、名家名曲名舞、中央领导和与会代表合唱歌曲几个部份。禁不住，我一次次打开手机让我的家人和朋友同我一起倾听人民大会堂的歌声。当剧作家苏叔阳朗诵巴金的《我为什么写作》第一句“我是由一个读者成长为一个作家”的时候，我陷入了深深的回忆。想当年在我的故乡北川我还是个少年的时候，有一天听说有个叫克非的作家来我们北川治城拍电影《春潮急》，我爬了几面坡，翻山越岭想去看看作家是个什么样子？最后我好不容易赶到党校挤进人群，从一个门缝里看见了克非老师，虽然看见的只是他的后脑勺，也让我激动了好半天。我又想到小时候看电影《达吉和她的父亲》时那种对作家的崇拜简直五体投地，直到后来我也进入了写作行列，每次见到高缨老师时，都还高山仰止，禁不住拉着他的手诉说儿时的情怀……<br />
&nbsp; &nbsp; 当然，联欢会最令人兴奋的还是胡总书记的节目。记得上一届联欢会时，胡总书记由歌唱家杨洪基陪唱了一首《我骑着马儿上北京》。这次是由吴雁泽、李双江刘斌、刘秉义四位歌唱家陪他唱了两首经典的情歌，一首是《在那遥远的地方》，一首是《莫斯科郊外的晚上》。当主持人姜昆宣布，胡总书记将上台与艺术家一同表演节目时，会场响起了雷鸣般的掌声。在赵碧璇的钢琴伴奏下，总书记健步走上舞台与艺术家一起唱起了那脍炙人口的青海民歌《在那遥远的地方》。一曲终了，全场掌声、喝彩声不断。余音未散，主持人姜昆问大家，总书记唱得好不好？下面齐声回答，唱得好！姜昆又问，请总书记再来一个要不要？要！代表异常兴奋！姜昆又向四位歌唱家说，你们能不能小声点，平日里听你们的歌太多，今天晚上我们要听总书记的歌声。全场大笑，连总书记也忍俊不禁……“深夜花园里，四处静悄悄”在熟悉的旋律中，总书记的歌声再次响起。在座的代表们合着节拍，轻轻拍手，晚会一下达到了最高潮。唱到结尾时，几位歌唱家的声音渐渐隐去，只有总书记一句“但愿从今后，你我永不忘，莫斯科郊外的--晚--上……”萦绕在人民大会堂。<br />
&nbsp; &nbsp; 晚会结束后，总书记和总理要离开演播厅了，不少代表挤在过道上伸出手去想和他们握一握手，总书记和总理没有一点架子，凡是能够得着的，都握住了。有一位女代表，眼疾手快一把拉住了温总理的手，久久不舍松开，就那样使劲地摇啊摇！松手后，十几只手又一下抓住她的手摇啊摇……大家激动得泪流满面。领袖在代表们中间其乐融融，亲民和谐，真乃一种大情大义呀！<br />
 <br />
五、关于合影签字<br />
 <br />
&nbsp; &nbsp; 由于一些特殊原因，这次会议没有安排中央领导同两会代表一起合影。但各代表团或集体或个人都积极地留影纪念。还有很多人去找自己心仪崇拜的作家和艺术家合影。我小时候看过表演艺术家孙道临的《永不消逝的电波》，一直想和他合个影。上届会议时，我已见过他了，只是当时没有相机。这次又没见着他，挺遗憾的。正好那天下午听完温总理报告后在人民大会堂出口处碰上了《白毛女》和《党的女儿》的主演田华，她也是我小时候就崇拜的偶像，于是便跟了上去。许是已有很多人找她合影她有些累了，随同她的人不让拍。我灵机一动，大声向田华道：田华老师，我是党的女儿的女儿，请让我跟您合个影好吗？田华侧身眼睛一亮，朝我笑了笑，表示应允。我立即挽住她，我们团的杨秘书长马上为我们定格了那个欣喜的瞬间。<br />
&nbsp; &nbsp; 在我们作代会这边，有两位作家也定格在了我的脑海。一位是史铁生，一位是张海迪。那天是作协的大会，我们匆匆赶往会场，突然一辆轮椅车从我身边驶来，我侧身一看是《伏兮伏兮》的作者刘恒推着史铁生，心里油然一种不可言喻的感动，我用心拍下了那个难忘的镜头。另一个晚上我的律师福堂和制片人文萱同时到北京饭店看望我。没想到他俩一起提出要找张海迪合个影。我带他俩到饭厅找到海迪说明来意，没想到海迪非常乐意。两位朋友对海迪说，我们上高中时读的课本就是您的书，您一直是我们的榜样。我看见海迪美丽的眼睛闪着幸福的泪花，直说谢谢！两位朋友站在她的轮椅后，挨着她的头，我也为他们留下了一缕人间难得的情愫。<br />
&nbsp; &nbsp; 出席这样的盛会，难免没有各种形式的交流。文友之间不留言不签字倒也罢了，可是面对媒体和学生就有些不好办法了。那天一个记者兼崇拜者来我们团找阿来留言，正好我和阿来坐在一起讨论。阿来大概写了句“一个国家便是一个人，一个人便是一个国家……”颇带禅意的话，那姑娘又把笔递给我让我也写一句。我随便写下了“我用母语为我的母土讴歌”。没想到她非常满意地说，主旋律耶！我自嘲道：还算是吧，不过是艺术的主旋律。后来我母校北师大和北大有几个学生来找我留言签字。我记得我给那些莘莘学子留的言是：“高峰的基座是浅丘，大师的根苗是学子”；“低调做人，精道为文。”；“逃名而名我追，避声而声我随。”。后来高缨老师也拿着个本，找人留字。我和他开玩笑道：是为您儿子还是孙子？他很认真道：当然是孙子。我于是也很认真地在他本上写道：爷爷是你的绩优股，你是爷爷的期货。他笑了，说：碧芳，你真俏皮！<br />
&nbsp; &nbsp; 此外，好些出版社的编辑、出版家，还有门类的评论家，艺术家都抽会议空档互相交流、勾通、增进了解和友谊。</div>

							
							
							
							
