													<h2>《在东莞》作者被释放 警方办案程序遭质疑</h2>
						
						
																											<div id="postmessage_849920" class="t_msgfont">潇湘晨报9月30日报道 湖南益阳出生的袁磊，因网络小说《在东莞》被广东东莞警方刑拘，罪名“涉嫌传播淫秽物品”。然而，广东省公安厅昨日上午通过网络发布的信息表明，东莞警方从立案到刑拘犯罪嫌疑人袁磊，都一直没有审查认定这本小说为“淫秽物品”。“渭南书案”律师周泽痛批东莞警方“违反办案程序”。<br />
<br />
昨日上午10时许，袁磊妻子阮海梅来到东莞厚街公安分局，几名警察接待了她。警察只提到了涉案小说里面的几点内容，比如小说引用了网络上一首根据《北京欢迎你》改编的桑拿版《东莞欢迎你》的歌词。究竟是什么内容让警方认定小说为淫秽物品，阮海梅没有获得解答。“感觉警察也是奉令行事。”一直拖到下午1点才出来的阮海梅说。<br />
昨天上午，“平安南粤”――“广东省公安厅网络问政平台，网络发言人互动平台”发布了一条关于袁磊案的微博，称“若涉案小说经审查后不能认定为淫秽物品，原立案单位也会依法撤销案件，并妥善做好后续处理工作。”随后，网民民意出现强烈反弹，网民认为这说明东莞警方“抓错了”。中午时分，“平安南粤”删除这条微博，直到17点46分，“平安南粤”发布微博--“目前，警方正深入调查袁磊涉嫌传播淫秽物品一案，请各位关注此案的网友耐心等候相关部门第一时间发布的信息。”已经没有了任何实质内容。<br />
<br />
“渭南书案”的律师周泽称，广东省公安厅发布的信息表明，东莞警方根本没有确认涉案小说是“淫秽物品”，就先立了案去抓人，这明显“违反办案程序”。周泽称，警方应该先确定涉嫌的犯罪事实，这是立案的前提。而现在东莞警方的所为表明，警察可以“先假定你有罪，把人刑拘后再确定证据”。<br />
<br />
昨晚近12时，本报记者获悉，袁磊已经被放出来，袁磊昨夜正在回家路上，袁磊的妻子阮海梅向记者证实此点。<br />
