													<h2>HumanEvents：19-20世纪最有害的书目</h2>
						
						
																											<div id="postmessage_18" class="t_msgfont">美国“人事”杂志（HumanEvents）邀请了15位保守派学者和公共政策领袖组成一个评委会，来评比19到20世纪世界“十本最有害的书”（TenMostHarmfulBooks）。每个评委可以推荐一定数量的候选书目，然后给全体评委推荐的所有候选书目打分：一本书如果被一个评委列为第一名（最有害的书），得10分，第二名得9分，依此类推。<br />
　　<br />
　　1，共产党宣言（TheCommunistManifesto）<br />
　　作者：卡尔・马克思（KarlMarx），弗里德里希・恩格斯（FreidrichEngels）出版时间：1848得分：74<br />
　　点评：马克思和恩格斯分别于1818年和1820年出生于德国，是共产主义的精神教父。恩格斯起初是个左派富家公子：他继承了收入丰厚的纺织厂，马克思人生的大部分靠他资助。1848年，二人合着了“共产党宣言”，作为他们所属的“共产国际”（CommunistLeague）的行动纲领。共产党宣言把人类历史看作剥削人的有产者和受剥削的工人之间的阶级斗争，号召公认起来革命，从而消灭私有财产、家庭和国家，建立一个无产阶级的乌托邦（proletarianUtopia）。苏联（SovietUnion）这个“邪恶帝国”（EvilEmpire）把共产党宣言付诸实践。<br />
　　<br />
　　2，我的奋斗（德文：MeinKampf，英文：MyStruggle）<br />
　　作者：阿道夫・希特勒（AdolfHitler）出版时间：1925-26得分：41<br />
　　点评：我的奋斗（MyStruggle）最初分成两个部分，分别发表于1925年和1926年。当时希特勒因为领导纳粹“褐衫党”（BrownShirts）搞著名的“啤酒馆政变”（BeerHallPutsch）而被投入监狱，那次行动旨在推翻巴伐利亚政府（Bavariangovernment）。希特勒在书里阐述了在德国实行种族主义（racist）、反犹主义（anti-Semitic）的构想，这个纳粹纲领直接导致了第二次世界大战（WorldWarII）和对犹太人的大屠杀（Holocaust）。他设想通过大规模屠杀犹太人、对法国的战争、以及以后的对苏联的战争来为德国在东欧开辟“生存空间”（livingroom）。这本书开始并没有引起多少注意，但在希特勒上台之前已经很流行。据SimonWiesenthalCenter的资料，到纳粹德国战败的1945年，此书发行量高达1千万册。<br />
　　<br />
　　3，毛主席语录（QuotationsfromChairmanMao）<br />
　　作者：毛泽东出版时间：1966得分：38<br />
　　点评：毛泽东死于1976年，曾经在第二次世界大战之前、二战期间和二战后担任红军的领袖，领导为争夺对中国的控制和反gong的蒋介石（ChiangKai-shek）部队进行的内战。1949年内战获胜，他创建了中华人民共和国，以共产主义奴役世界人口最多的国家。1966年他发表“毛主席语录”，又被称为“红宝书”（TheLittleRedBook），作为他发动的旨在把中国共产党和中国社会推回他设想的方向的“文化大革命”的一个工具。通过强制性配发，“毛主席语录”印刷了几十亿册。西方的左派被书中马克思主义的反美论调所倾倒。毛泽东写到，“结束帝国主义主要是美帝国主义的侵略和压迫，是全世界人民的任务”。<br />
　　<br />
　　4，金赛性学报告（TheKinseyReport）<br />
　　作者：阿尔弗雷德・金赛（AlfredKinsey）出版时间：1948得分：37<br />
　　点评：阿尔弗雷德・金赛是印第安纳大学（IndianaUniversity）的动物学家（zoologist），他在1948年发表了论文“男性性行为”（SexualBehaviorintheHumanMale）。五年以后，他又发表了“女性性行为”（SexualBehaviorintheHumanFemale）。这两个报告科学的曲解乱交（promiscuity）和性异常（deviancy）属于正常行为。当2004年一个关于金赛的电影上映时“华盛顿时报”报道说：“1948年发表的金赛报告…震惊了美国，里面说美国男性在性行为上很狂放，按照上世纪40年代的法律，95％的男子都可以认为犯下了性侵犯（sexualoffense）。金赛报告包括男孩性行为――甚至婴儿性行为――的记录，说37％的成年男子曾经有过至少一次同性性行为…这本1953年的书还包括四岁以下女童性行为的记录，建议说成年人和儿童之间的性行为可能是有益的。”<br />
　　<br />
　　5，民主主义与教育（DemocracyandEducation）<br />
　　作者：约翰・杜威（JohnDewey）出版时间：1916得分：36<br />
　　点评：约翰・杜威（1859－1952），“进步论”哲学家（progressivephilosopher），领导了美国生活的世俗人道主义（secularhumanism）运动，曾任教于芝加哥大学（UniversityofChicago）和哥伦比亚大学（UniversityofColumbia）。他参与签署了“人道主义宣言”（HumanistManifesto），反对传统宗教和道德的绝对性。在“民主主义与教育”一书里，他用华而不实含糊不清的散文体，他贬低重视传统品质发展和教授硬知识的学校教育，代之以鼓励讲授“思考技巧”（thinkingskills）。他的观点极大的影响了美国教育的走向――特别是公立学校――帮助孕育了“克林顿一代”（Clintongeneration）。<br />
　　<br />
　　6，资本论（德文：DasKapital，英文：Capital）<br />
　　作者：卡尔・马克思（KarlMarx）出版时间：1867-1894得分：31<br />
　　点评：马克思生前只看到“资本论”这一大部头著作的第一卷得以出版，他的赞助人恩格斯编辑出版了马克思起草的另外两卷。资本论强行把资本主义套入毫不相称的马克思的历史唯物论中，把资本主义描绘成人类社会发展的一个丑陋阶段，资本家不可避免而且毫无道德的通过致富最低工资压榨工人，来获取最大利润。马克思的理论认为最终的结果就是全球无产者的大革命。他没能预知21世纪的美国：一个建立在资本主义和代议制政府基础上的自由富裕的社会，一个全世界都羡慕并且努力模仿的社会。<br />
　　<br />
　　7，女性的奥秘（TheFeminineMystique）<br />
　　作者：贝蒂・弗里丹（BettyFriedan）出版时间：1963得分：30<br />
　　点评：贝蒂・弗里丹生于1921年，她在“女性的奥秘”一书里贬低传统的“居家母亲”（stay-at-homemotherhood），把那比作生活在“舒服的集中营”（acomfortableconcentrationcamp）――那是一种使女性贬值并否认她们真正的自我实现的生活方式。她后来成为“全国女性组织”（NationalOrganizationforWomen）的首任主席。她自己本来的职业很说明问题，她不是个“居家母亲”，而是一个左翼记者。正如DavidHorowitz为Salon.com写的一篇关于DanielHorowitz所著“弗里丹和女性的奥秘的制造”（BettyFriedanandtheMakingoftheFeminineMystique）一书的评论里说的（DavidHorowitz和DanielHorowitz没有亲缘关系）：作者（DanielHorowitz。译者注）记录了“弗里丹从大学时代开始，直到她三十五岁左右，一直都是一个斯大林派马克思主义者（StalinistMarxist），冷战期间埋伏在美国的第五纵队头头的政治密友，某个时期甚至是一个年轻的共产党物理学家的情人，那个物理学家和奥本海默（J.RobertOppenheimer）一起为柏克利大学反射实验室的原子弹项目（atomicbombprojectsinBerkeley’sradiationlab）工作。”<br />
　　<br />
　　8，实证主义哲学（TheCourseofPositivePhilosophy）<br />
　　作者：奥古斯特・孔德（AugusteComte）出版时间：1830-1842得分：28<br />
　　点评：孔德出生于一个天主教保皇党家庭（royalistCatholicfamily），幸免于法国大革命（FrenchRevolution），他被判了自己的政治和文化继承，在青少年时期就宣布“我自然而然的停止相信上帝”。后来，他在六卷本巨著“实证主义哲学”里首次介绍了“社会学”（sociology）这个词。他创立了一个理论认为人类意识已经发展到了超越“神学”（theology，相信上帝掌管这个宇宙的信仰）的阶段，通过“形而上学”（metaphysics，这里的定义是法国大革命所相信的没有上帝的、抽象意义上的“正义，rights”），达到“实证主义”（positivism）。那样人类自己通过科学发现，可以决定事物应该的走向。<br />
　　<br />
　　9，善恶之外（BeyondGoodandEvil）<br />
　　作者：弗里德里希・尼采（FreidrichNietzsche）出版时间：1886得分：28<br />
　　点评：大学校园里潦草的涂鸦写道：“‘上帝死了’－－尼采”，后面跟着的是“‘尼采死了’－－上帝”。“上帝死了”（Godisdead）最早出现在1882年出版的“同性恋科学”（TheGayScience）一书里，但它成了四年后出版的“善恶之外”一书的基本主题。尼采在书中指出人被不道德的“权力欲”（WilltoPower）所驱动，“超人”（superiormen）会横扫宗教激发的道德准则――他相信那和任何其他道德准则一样都是人造的――然后确立一套用来帮助他们统治世界的自己的道德法则。“生命的本质就是占有，伤害，压倒陌生人和弱者，压迫，严惩，被迫接受自己的样式，兼并，以及最温和的、至少也要攫取”，尼采写道。纳粹热爱尼采。<br />
　　<br />
　　10，就业、利息与货币的一般理论（GeneralTheoryofEmployment,InterestandMoney）<br />
　　作者：约翰・梅纳德・凯恩斯（JohnMaynardKeynes）出版时间：1936得分：23<br />
　　点评：凯恩斯属于英国精英――在依顿公学（Eton）和剑桥大学（Cambridge）接受教育――这个剑桥大学的自由派经济学教授在“大萧条”（GreatDepression）期间写了“就业、利息与货币的一般理论”。这本书是不断膨胀的政府所采用的药方。当一个商业周期（businesscycle）使某个行业面临萎缩的威胁、进而导致工作机会的萎缩时，凯恩斯认为政府应该增加预算赤字，借款花费来刺激经济活动。富兰克林・罗斯福总统采用了这个观念作为美国的国策，现在美国政府每年的预算使26亿美元，负债80亿美元。<br />
