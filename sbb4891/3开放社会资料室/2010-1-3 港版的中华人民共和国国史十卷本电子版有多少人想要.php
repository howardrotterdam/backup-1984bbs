																											<div id="postmessage_412142" class="t_msgfont">2008年5月，在香港发行的十卷本《中华人民共和国史》(已出版六卷)，无疑是中国学术界的一件大事。这套著述由香港中文大学当代中国文化研究中心组织编写并出版，不仅史实详尽，规模空前庞大，而且可谓私撰国史，意图以正视听。这套著述主要涵盖了以毛泽东时代为主的1949至1981年的政治史，与当代很多中国人的政治社会生活曾休戚相关。如果说学者们对这套国史中事件梳理之清晰与档案数据运用之细致会留下深刻印象，那么对于经历过毛泽东统治时代的普通读者而言，这套国史也许能让他们解开一些影响过自己人生的重大历史事件之谜团。<br />
<br />
　　对于国史，中国历来有官修与私撰两大方式。官修为后人留下了二十四史，而私撰因其可对历史事件作独立记录与评点，有时比官修来得更为真实和重要，正如孟子所谓：「孔子成《春秋》而乱臣贼子惧!」<br />
<br />
　　这套国史继承了中国私撰史学的传统，由非官方学术机构主持，邀请一批卓有成就的学者，摒弃政治意识形态准绳，以尊重史实的态度写作，在计划总数600万字的著述里，对毛泽东时代许多极具争议的重大事件作尽可能的厘清。由于这套国史在香港出版，作者无须过于担心书中语句或资料会被政治权威删改，得以基于自己对历史事件的认知和感悟而写作，因此，与在中国大陆出版的共和国史著作相比，这套国史能够更加完整和真实地重现历史，其学术价值也相对较高。<br />
<br />
　　这套国史最引人瞩目之处是对档案数据的利用。在共和国时代，长期以来国史无法私撰。除了国家对史学著作的政治审查外，国家机构对历史档案的保密管理也根本性地限制了国史研究。尽管近十年来政治环境有所宽松，但由于国家的意识形态松弛度和档案开放度依然极为有限，学者迄今为止所撰写的共和国史很难不沿袭官方正统的声音。对于大跃进与文革等一些被国家视为禁忌的重大课题，他们不但无法作透彻的研究，更无法畅所欲言发表看法，只能眼看着西方学者的专著一本本的出版。<br />
