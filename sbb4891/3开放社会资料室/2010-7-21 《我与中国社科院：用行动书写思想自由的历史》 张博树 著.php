													<h2>张博树新书《我与中国社科院》在香港出版</h2>
						
						
																											<div id="postmessage_751255" class="t_msgfont"><a href="http://www.chinesepen.org/Article/bhdt/201005/Article_20100514044735.shtml" target="_blank">http://www.chinesepen.org/Articl ... 0100514044735.shtml</a><br />
<br />
【独立中文笔会自由写作委员会5月13日香港讯】在“六四”21周年纪念即将到来之际，于一个多月前被中国社科院当局驱除的原中国社科院哲学所学者、知名宪政学家张博树，其撰著的《我与中国社科院――后极权时代思想自由抗争史的一段公案》本周末在香港由晨钟书局出版、田园书屋发行推出上市。张博树系独立中文笔会会员，现任理事会候补理事、自由写作委员会委员，该书作为独立中文笔会文库第8辑《张博树文集》第4册出版。<br />
<br />
1989年“六四” 期间张博树是中国社科院研究生院的博士生，他在学潮高峰期领衔起草的《中国向何处去？――中国社科院博士研究生致中共中央、全国人大常委会、国务院的公开信》，要求抵制腐败、推动中国政治民主，引起重大反响，埋下20年来在中国社科院被视为“异类”的“祸根”。1991年张博树获中国社科院博士后在哲学所任研究人员。1993年，他在香港《二十一世纪》杂志发表《批判思潮与建设意识之我见》，文涉“六四”敏感主题，由此开始受到社科院当局的打压和排挤；一路走来，张坚持公共知识分子的良心立场，坚持捍卫思想自由、言论自由的精神原则，在中国宪政转型研究和20世纪中国专制主义批判等原创学术领域建树甚丰；自他的重要著述《中国宪政改革可行性可行性报告》、《从五四到六四》、《解构与建设》等出版赵诨チ网广为传播后，中国社科院当局加紧对他在生存权、工作权等方面的迫害，直至去年12月被“限期三个月调离”，今年3月被驱除出社科院。《我与中国社科院――后极权时代思想自由抗争史的一段公案》一书实录的正是作为独立学者、公民思想家的张博树与作为官方御用机构的中国社科院长达20年的博弈史，更是一部难得、罕见的“六四”21年以来知识界良心抗争的个人史。<br />
<br />
曾任中共军事学院出版社社长、以“批毛”巨着《红太阳的陨落――千秋功罪毛泽东》而闻名的知名传记作家辛子陵对张博树作出高度评价，昭铣庵泄社科院当局的猥琐行径：“张博树是一个学识渊博、思想深刻、卓尔不群的中年学者。在宪政学界的地位，他可以与在经济学界长期被视为异类，受批判、受打压甚至收监入狱，后来又受尊敬、受推崇成为知识份子楷模的顾准、孙冶方相伯仲，他可能成为中国的孟德斯鸠。培养这样一个人才不容易，毁灭这样一个人才易如反掌。中国社会科学院应该是海纳百川的学术殿堂，不应该是唯我独尊、自私偏狭的官僚衙门。处理张博树，陈奎元院长躲在幕后，让哲学所出面，哲学所正所长还不出来，叫两个副所长出来跳加官，这本身就说明了他们的心虚胆怯。……在社会科学院，最应该被开除的是陈奎元同志，希望您自觉地引咎辞职。”<br />
<br />
以编纂“思想史上的失踪者”的著述《顾准日记》、《遇罗克：遗作与回忆》、《王申酉文集》而知名的北京学者丁东则称誉张博树“是一位颇有思想家气质的中年学者，本身的经历在当今中国就富于传奇色彩。……近几年，张博树的力作《从五四到六四：20世纪中国专制主义批判（第一卷）》、《中国宪政改革可行性研究报告》陆续问世，回答了中国走向民主宪政的一系列重大问题，以思维的缜密、逻辑的坚实、文字的干净、史实的清晰、说理的透彻征服着越来越多的读者。”<br />
<br />
张博树新着《我与中国社科院》一书由浦前编，既具有论战性、时事性，同时又具有文献性、史料性，该书分为“正编”、“副编”、“另册” 三个部分：“正编”部分，是“公案”肇因及“公案”发生的前前后后，张博树的多篇重要文章及相关资料文献，历时性地、全景性地客观呈示了这一场自由与专制、良心与强权的博弈史、抗争史。“副编”部分，辑入“公案”发生前后，知识界、传媒界及互联网世界对张博树其人、其事、其著的诸多评说与报道，作者中近有张博树在中国社科院相知、相惜的同事、同行，远及互联网上网民路见不平、仗义声援的佚名之侠士。中国社科院的两份文档被编者不客气地打入了“另册”。读者从其中一份严重违宪的“红头文件”《中国社会科学院关于加强政治纪律建设的决定》，可窥“中国社会科学的最高殿堂”其内质的可怖与可悲之一斑。<br />
<br />
张博树目前应台湾东吴大学之邀，正在该校政治学系讲学。东吴大学张佛泉人权研究中心与独立中文笔会自由写作委员会将于5月20日在台北联合举办“张博树《我与中国社科院》新书发布暨两岸精神创造、知识生产者的学术自由座谈会”。包括黄默、周渝、丘延亮、徐斯俭、吴介民等在内的多位台湾的重要学者、知识分子将出席与谈。两岸处在社会转型的不同位阶与面向，却都面临关涉精神创造、知识生产者思想自由、学术自由践行的重要议题，张博树的新书、张博树的“公案”是一个继续提起争议、鼓励争议的引子，有助于两岸建制内外及社会各界，在汇入现代宪政文明潮流的大趋势中，普世价值认知差异的弥合，及共识的构建。</div>

							
							
							
							
