													<h2>评《我与中国社科院》</h2>
						
						
																											<div id="postmessage_751261" class="t_msgfont"><a href="http://www.rfa.org/cantonese/features/bookclub/Zhangboshu-05212010135822.html" target="_blank">http://www.rfa.org/cantonese/fea ... 05212010135822.html</a><br />
作者：刘云<br />
<br />
中国社科院一直予人一个复杂的形象，它既是学术研究的机构，但又是中共政权体制下，成为它意识形态的部门，肩负宣传政策及方针，为党服务的责任。观乎其直接隶属于国务院，属中央部委级别的单位，其地位的超然性便可想而知。<br />
<br />
正因一所学术机构背负著一项重大的政治责任，对从事学术研究的学者而言，无端被添加额外负担，不过，对忠于学术自由的独立学者而言，这些负担并未把他们的嘴巴封上，亦未把他们的思想变为空白，更未有把他们的笔截断，即使，他们的饭碗确实已被砸过稀巴烂。<br />
<br />
今集要介绍的一本好书，作者是不畏于这体制，早阵子更成为新闻及学术界焦点人物的独立学者张博树，他刚付梓出版的新书《我与中国社科院――后极权时代思想自由抗争史的一段公案》。公案之意，正是他一直与体制下规订的「政治纪律」进行多年博弈，最终遭中国社科院解除雇佣关系的事件。<br />
<br />
由晨钟出版社出版的《我与中国社科院》一书分「正编」、「副编」及「另册」三部分。「正编」部分，是公案的肇因及发生的前前后后的事情。「副编」部分则辑入学术界、媒体及公民社会对事件及其人的意见；最后的「另册」就是扼杀学术自由的罪魁祸首就是社科院党组发出的「政治纪律」即「中国社会科学院关于加强政治纪律建设的决定」的「红头文件」。书内亦收录了张的多篇重要文章。<br />
<br />
张博树在书中指，这些「政治纪律」矛头不独针对学者，对官员和社科院所有其他的员工都予以规管。而这类规定同样在其他研究机构、大学、党校、出版社、杂志社之类的地方发挥作用。至于，政府及党务机关、国有企业及各类事业单位同样都有类似的「政治纪律」缚紧工作人员的脑袋及舌头。该文件开首便这样说：「必须看到，国内外敌对势力始终把中国社会科学院作为进行意识形态渗透的重点对象，他们从来没有放松过对干部和学者进行「西化」和「分化」的活动。近些年来，受各种错误思潮的影响，我院违反政治纪律的行为时有发生。因此，加强政治纪律建设，是全院一项极其重大的政治任务。」<br />
<br />
《决定》明言：“加强政治纪律建设的基本任务是：通过维护法律、纪律和规章制度，保证党的路线、方针、政策和国家法律法规的贯彻执行，保证各单位、各部门和党员、干部、学者在政治上与党中央保持一致。”<br />
<br />
张博树认为这些说话，一点含糊也没有，就是要确保「在政治上与党中央保持一致」，而且，因为这「一致性」而导致当年发生的连串人间悲剧包括反右、大跃进、大饥荒、文革、六四及镇压法轮功。至于，「国内外敌对势力始终把中国社会科学院作为进行意识形态渗透的重点对象，他们从来没有放松过对干部和学者进行「西化」和「分化」的活动」云云，他则认为，这正是中共典型的意识形态冷战语言和敌对思维语言。这些话不能对外公开表述，只能向外界讲要「构建和谐世界」。这「内外两套」语言的体系，这是党多年来的特点。<br />
<br />
书中胪列了九项的「政治纪律行为规范」，当中包括：「必须坚持马克思主义在哲学社会科学研究中的指导地位，不得公开发表反对和违背党的基本理论、基本路线、基本纲领的言论」；「凡我院主办或协办的各类学术会议，不得邀请有违反四项基本原则错误倾向和不良意图的人员参加，对参会论文要认真审查和把关。对于院外有政治错误和政治上别有用心的组织和个人举办的各类会议，不得参加」；「在对外学术交流活动中，必须严格遵守《中国社会科学院对外学术交流规定》。我院人员在境外出版书籍、在境外媒体和出版物发表文章或言论前，须报所在单位领导审阅同意。参加境外学术活动，邀请境外人士来访，须按规定程式审批」、「不得编造传播政治谣言和反动、错误观点；不得参与非法组织或个人发起的有政治违纪内容的签名、串联、‘纪念’等活动」及「不得违反党和国家的民族宗教政策，不准发表影响社会稳定的言论」等。<br />
<br />
简言之就是，从言论、结社、会议、出版、接受媒体访问、国际学术交往各个方面严格限定，不得批评执政党，不得批评现行体制，不得公开发表反对和违背党的基本理论、基本路线、基本纲领的言论。<br />
<br />
张认为这些规限，正好是现代专制主义等于现代蒙昧主义的最佳注脚。<br />
<br />
张的敢言，其实并非在近数手间才出现，始自他在一九九三年于香港向一本杂志发表了一篇学术短文，内里提及「六四」。在该篇文章，张博树是这样写的『「六四」这样的大事变亦仍「矢志不移」。这是由于社会禁忌已经成功地把人的批判意识囚禁起来。而我则相信，如果不将这个「批判意识之囚」解放出来，21世纪的中国仍然没有希望！』<br />
<br />
未料，文中几句涉及六四的话，触及学院的神经线，特别为他安排了「帮助会」，之后，党委书记傅青元更找他谈话，明确表示学院很重视。<br />
<br />
但是，强调要独立思维的张博树又岂会听从，往后仍旧发表多篇独立见解的学术性文章，有些更触及共产党认为极敏感的议题如西藏等，当然，随之而来的又是被领导「规劝」及「威吓」，但是，更直接的就是六次提交升级的申请，无一次接纳。令张博树廿年间在社科院的日子，仍然停滞在入职时的职级「助理研究员」。<br />
<br />
书里更首次披露，在「六四」十五周年时，张博树又撰写了一篇「为六四正名，不应再拖下去」的文章交予《北京之春》发表，之后，当然，又招来社科院关注，副院长李慎明与张约谈，那次会谈，张博树跟中国自由派学者徐友渔一起赴约，因为徐参与了呼吁重新评价六四的集体签名。李慎明在席间除了要求不披露对话内容外，更先以东欧和俄罗斯当年分裂的问题作为引子，表达了他认为中国当年对六四问题的处理是对的讲话，更谓，否则中国也会像前苏联一样的下场。<br />
<br />
读者看《我与中国社科院》时，不难萌生一种感觉，就是在看著一部令人「惊心动魄恐怖」的悲剧故事，当然，这种惊心动魄恐怖不是真正的恐怖片，当中没有电影中会突然跳出来吓观众的鬼怪或血腥暴力的戏剧原素，而是那种阴霾、不知何时及如何发生那预见的结果，及人性屈服于权威、拒予讲「不」、害怕麻烦的驯服心态的悲凉性，俨如现代版的电影「一九八四」。<br />
<br />
在这阴霾的氛围中仍能保持清醒、独立兼敢言的张博树谦称，不觉得自己如何的勇敢，因为国内有众多自由主义的学者的见解跟他也是一致的，只不过，每个人都有自己的考虑，有自己的选择而已。<br />
<br />
他承认，在经历每一次的事件里，他都有很多考虑，更明白在一个落后的体制里，过程中必定有风险，更肯定有代价。但是，他清楚自己的选择，亦甘愿承担，因为他从中获得形而上的提升，纵使形而下的生活受到影响。<br />
<br />
他h：我觉得做人至少有做人的基本原则, 即是该为自己认定的一种方向或生活目标而努力。<br />
<br />
他承认自己较其他的学者比较，好似跑得远了一点，原因跟他研究的学科关系，因为他研究的题目就是中国现代专制主义的传承、演变，及须要对其作出深层的分析等。所以，他认为这正好注定他的思考及表达模式，不会转弯抹角，而是坦率直白面对问题的核心。<br />
<br />
可是，张博树用肯定的口吻说，他所做的是对国家及民族有利的。<br />
<br />
他说：我做是因为对国家及民族是有利的,我们在这转型过程中,须要有知识份子来承担这角色, 须要有人多多少少付出一点牺牲。我当然不是说自己在这议题上有多么高尚的道德，我只是由于自己的工作岗位的性质，我自己也做好思想准备，所以，作出这样的牺牲也是可以承担的。<br />
<br />
中国境内欠缺独立思考的学者？答案肯定不是，只不过，众人头顶有把「刀」,而这把箝制思维的刀迄今一直存在，一直在人的内心里发挥影响力。可是，刀也有生锈的时候，也有「死敌」──盾──的存在，问题是你愿意撑起这把盾吗？</div>

							
							
							
							
