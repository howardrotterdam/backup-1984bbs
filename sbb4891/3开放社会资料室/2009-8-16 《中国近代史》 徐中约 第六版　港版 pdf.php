我认为现在的青年如果没有时间读古代史和世界史，至少应该仔细研读一下这本中国近代史，能看到很多社会现状的根源。<br />
<br />
没听说过这本书的同志先看看贺卫方的博客：<br />
<br />
贺卫方：一国两版――或“肢解名著，情何以堪？” <br />
　　　　　　 <br />
　　香港中文大学出版社的两卷本。译校者与大陆一卷本相同，内容却大不一样 <br />
