　　旅美历史学家徐中约先生的经典著作《中国近代史》终于被翻译成中文，并且在香港和大陆分别出版繁体字版和简体字版（计秋枫、朱庆葆译，茅家琦、钱乘旦校，徐中约审订。繁体版由香港中文大学出版社出版，简体版由世界图书出版公司出版）。我在多年前搜集林则徐翻译国际法相关资料时，曾拜读过徐先生关于近代中外关系史的大作，看到《中国近代史》中文版出版，译校者足够专业，而且又是作者亲自审订，就毫不犹豫地掏出六十余元买了一本。 <br />
　　回来后翻阅，看到书前有出版者署名的一篇“出版前言”，其中说“作者浸淫西方学术多年，其理论立场、观点、思想倾向及批评标准与我们不尽相同。经版权所有者授权，我们对本书的一些内容进行了适当而审慎的编辑处理，其不妥之处还请读者见谅。”于是就对比了一下不同版本。真是“不比不知道，一比吓一跳”：本来的四十二章，居然遭到大砍大杀，只剩下二十九章。关于中华人民共和国，“第一个十年”之后，就直接进入到“中国重入国际社会”，中间近三十年的历史完全删除殆尽！ <br />
　　整章连片地砍杀之外，还有不间断的字句删改。下面是作者全书最后一节“二十一世纪初的中国”的一部分，我用红色字标出删改的部分，可以看出这种所谓“适当而审慎的编辑处理”到了这怎样令人震惊的程度。 <br />
　　如果中国经济按年增长率8%的速度持续下去，到2020年它的经济力量可能变成世界第一。在政治上，中国政府正在寻求用一种结合的哲学取代行将朽木的共产主义意识形态【改为“一种统合的哲学”】。这种哲学看起来应类似一种包含着传统儒学、爱国主义、民族主义【和少许反映历史上王朝观念的“大中华主义”】所组成的混合体。与此同时，为了保持经济势头的强劲，政府积极推进市场经济体制，【并实施那种新型买办式的、以利润为中心的准资本主义】。对于不能盈利的国有企业，政府将出售给私人股东；为鼓励个人购买住房，政府通过抵押贷款予以资助。军事上的投入也一直在稳步【合理】进行，使中国在国际政治中不失为一支重要的力量。 <br />
　　中国共产党已经从一个革命组织变成了执政的政党。它的主要任务是发展经济，提高民众生活水平，并提高中国的国际地位。【它不再宣扬自己是“无产阶级的先锋队”，也不再推崇第三世界的立场，而是渴望将中国提升到第一世界的前列。的确，追求财富、权势和国际尊敬，已经取代了对马克思主义乌托邦的理念。】 <br />
　　【经由十五届党代会（1997年9月）和全国人大（1998年3月）所选定的党和政府领导人，大部分都是年纪更轻、教育程度更高的都市化的技术官僚。前四位领导人：国家主席Jiang Zemin、副主席Hu Jintao、总理朱F基、人大委员长LiPeng，都是大学毕业的工程师，他们将毫不犹豫地在二十一世纪将中国带入科学和资讯新时代。 党对人们生活的控制已大大放松，但依旧戒心十足地保持着它对政治权力的垄断。任何异议都是不允许的。一些异见人士在经过了长期刑罚以后以“保外就医”的理由驱逐到国外；宗教迫害依然存在。】 <br />
