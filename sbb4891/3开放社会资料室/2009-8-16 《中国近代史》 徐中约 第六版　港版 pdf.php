　　人民憎恶任何社会大动乱，【例如文化大革命，】或导致社会和经济的大混乱的秩序破坏，就像共产党垮台【改为“苏联解体”】后俄罗斯所出现的那样。他们【不主张在中国以暴力推翻共产主义体制，但】主张内部【和平】变革，使它能更清醒、更敏锐地应付时代的挑战。通过【微小】进步的积累过程，一个更自由【改为“美好”】的政治制度最终一定会出现。他们希望中国融入到世界主流之中。 <br />
