													<h2>美国《新闻周刊》独家专访温 全文</h2>
						
						
																											<div id="postmessage_1947" class="t_msgfont">上周在纽约参加联合国大会的中国总理温jiabao罕见地接受了《新闻周刊》记者Fareed Zakaria的独家专访。现年66岁的温jiabao以坦率和精通经济著称，在他主政期间，中国正处于历史上最快的增长时期。他在访谈开始时保证要“实话实说”，并请Zakaria随时插话，因为温“更喜欢对话，而非长篇大论。”访谈涉及的话题从西藏和天安门广场到达尔富尔和人权，从政治思想到美国大选，从当前的金融危机到中国民主的未来。节录如下:<br />
<br />
<strong>ZAKARIA：你如何看待这场影响美国的金融危机？</strong><br />
<br />
温：我们应该携手合作、共担危机。如果美国的金融和经济体系出现问题，那么不仅本国受到冲击，中国、亚洲乃至全世界都会受其影响。<br />
<br />
<strong>现在许多人认为中国的经济增长将大幅度减速。你认为这种情形会发生吗？如果会发生，你认为将出现什么后果？</strong><br />
<br />
连续30年，中国经济以年均9.6％的增长率成长，这是个奇迹。2003至 2007年间，中国达到了两位数的增长率――而消费价格指数的年增长率不到2％。<br />
<br />
中国一直采取积极的调控措施。我们以前关心的是防止经济过热，防止物价上涨导致通货膨胀。<br />
<br />
但是形势变化很快，［是因为］美国的次贷危机以及随之而来的混乱。我们目睹了外部需求的下降，中国的国内需求无法在短时期内大幅提高。［所以］我们确实存在经济放缓的危险。我们必须调整宏观经济政策。<br />
<br />
<strong>如果美国走向严重衰退，你认为中国经济能够继续增长吗？</strong><br />
<br />
从今年前8个月的统计数据来看，我们一直都在作这种努力。［但是］美国的萧条肯定会影响到中国经济。十年前中美贸易只有1026亿美元，现在这一数字飙升到3020亿美元，增长了1.5倍。美国消费需求的缩水肯定会使中国的出口受到冲击。美国的金融业与中国的金融业关系密切。如果美国的金融业出了问题，我们将会担心中国资本的安全。<br />
<br />
<strong>中国是美国国债最大的持有者――据说大约价值1万亿美元。你能打消［美国人的］疑虑，说中国永远不会把这作为某种形式的武器吗？</strong><br />
<br />
我们相信，美国的实体经济依然基础牢固，特别是高科技和基础工业。虚拟经济出了一些问题，但如果问题处理得当，经济依然有稳定的可能。中国政府希望看到美国持续发展，因为那将使中国获益。当然，我们关心中国在美国的资金是否安全无虞，但我们相信美国是一个值得信赖的国家。特别是在类似的困难时刻，中国一直向美国伸出援手。而且实际上我们相信这种援助之手将有助于稳定全球经济与金融，防止出现大混乱……我认为当务之急是要通力合作。<br />
<br />
<strong>很多人已视中国为超级大国，他们好奇：为什么中国不采取更积极的行动，政治解决诸如达尔富尔或者伊朗之类的问题？</strong><br />
<br />
我需要纠正你提问中的措辞，中国不是超级大国。虽然中国拥有13亿人口，虽然中国近年来相当快速的经济和社会发展引人注目，但中国还……有8亿农民，我们还有几千万人口生活在贫困中。我们需要全心全意、尽最大努力处理这些问题。这就是为什么我们要把重心放在本国的发展上，放在提高人民生活上。<br />
<br />
但是中国政府确实能够向苏丹政府或伊朗政府或缅甸政府施加压力，让他们不那么专制。中国与这三国都有关系。<br />
<br />
这让我想到你的第二个问题。中国是主持正义的国家，我们从不拿原则做交易。就以你刚刚提出的达尔富尔问题为例，中国一向倡导两条腿走路：中国是首批向达尔富尔派遣维和部队的国家之一；中国还是第一个援助苏丹的国家；我们还不断努力与苏丹各派领导人沟通，谋求和平解决达尔富尔问题。<br />
<br />
<strong>如果伊朗掌握了核武器，你认为会给世界带来危险吗？你认为世界应该采取什么预防措施？</strong><br />
<br />
我们不支持伊朗加大核步骤。我们认为，伊朗有权开发核能、和平利用核能，但应接受国际原子能机构的监督，而且伊朗不应发展核武器……我们希望以和平谈判来达到这个目标，而不是任意地诉诸武力或者武力恫吓。这就像人与人之间的关系，如果一个人把另一个人逼得走投无路，只会起到反作用。我们的目的是解决问题，而不是加剧紧张关系。<br />
<br />
我问你一个问题：你不认为中国为解决朝核问题所作的努力实际上一直有助于局势稳定吗？我知道要完全解决朝核问题需要时间。但我们所采取的方式已被证实方向正确。<br />
<br />
中国的努力已经受到美国和世界的赞赏，人们因此希望中国在其他地区采取同样的积极行动。<br />
<br />
我们在几年的谈判中获得了许多经验、得到了很多教训。六方会谈取得进展得益于各参与方的密切合作。<br />
<br />
<strong>达赖喇嘛说他现在想接受中国在西藏的统治。为什么你不直接与他谈判、一劳永逸地解决这个问题？</strong><br />
<br />
达赖喇嘛是宗教领袖，在西藏地区必然拥有影响力。他不是普通的宗教人士。达赖喇嘛建立的所谓流亡政府实行的是神权统治。这个所谓流亡政府的目的是把西藏从中国分裂出去。达赖喇嘛在全世界宣扬大西藏地区的自治。他想把所谓的大西藏地区从祖国分裂出去。美国有许多人不知道这个地区有多大；它包括西藏、四川、云南、青海和甘肃，占中国国土的四分之一。<br />
<br />
几十年来，我们的政策［一直是］，只要达赖喇嘛愿意承认西藏是中国不可分割的领土、只要达赖喇嘛放弃其分裂活动，我们就愿意与他或者他的代表接触、会谈。现在，谈判的结果取决于他是否真诚。<br />
<br />
<strong>你希望看到达赖喇嘛采取什么行动来表示真诚？</strong><br />
<br />
放弃分裂活动可以表示他的真诚……当然，谈判会继续，根据谈判的进展，我们还会考虑提高谈判级别。<br />
<br />
<strong>温总理，中国在发展，正如你所指出的，30年保持9.5％的增长率――是有史以来增长率最高的国家。中国成功的关键是什么？采用的是什么方式？</strong><br />
<br />
［答案是］我们在1978年实行的改革和开放政策。我们解放了中国的生产力。我们有一个重要思想：社会主义能够实行市场经济。<br />
<br />
<strong>人们认为那是相互矛盾的。中国是如何让二者共同运行的？</strong><br />
<br />
在宏观经济指导下、在政府的监管下，让市场力量在分配资源方面充分发挥其基本作用。确保看得见和看不见得手都充分发挥作用。如果你熟悉亚当・斯密，你就知道他有两部名著。《国富论》分析了看不见的手：市场力量。另一部著作讲的是社会平等与公正；在书中，他强调了政府在国民进行财富分配时发挥监管作用的重要性。如果一个国家的大多数财富集中在少数人手里，这个国家难有和谐与稳定。<br />
<br />
<strong>有的美国人和欧洲人，特别是人权观察员，说中国近几年一直压制人权。他们说，他们希望奥运会能够把中国引向开放，但是压制反而更多了。你如何回答他们？</strong><br />
<br />
通过主办奥运会，中国变得更加开放了，任何不带偏见的人都会到这一点。言论自由和新闻自由在中国是受到保护的，中国政府重视、保护人权。我们把这些自由写进了中国宪法，并真诚地执行。我们认为，我们在人权方面表现得并不完美――确实在一些地方、一些地区存在问题。但是，我们在继续进步。<br />
<br />
<strong>当我到中国去的时候，我在饭店里把“天安门广场”几个字敲进我的电脑，我撞到了防火墙，就是人们所称的中国防火长城。如果连信息自由都没有，中国能算是进步的社会吗？</strong><br />
<br />
中国现在有超过2亿的互联网用户，中国的网络自由得到了很多人、甚至西方人的承认。为了保护国家安全，中国与世界上许多国家一样，采取了一些适当的限制措施。在中国的互联网上，你能浏览到许多批评政府的帖子。正是通过阅读这些网上的批评意见，我们努力发现问题、进一步改进我们的工作。我认为，一个体制或一个政府不应该害怕批评的意见或观点。<br />
<br />
<strong>你最喜欢的网站是什么？</strong><br />
<br />
我浏览许多网站。<br />
<br />
<strong>有一张你1989年在天安门广场的照片。你从处理那场事件的经历中得到了什么教训？你是否认为有必要停止政治改革？你认为在25年后，中国会出现候选人进行竞争的全民大选吗？</strong><br />
<br />
我相信，在推进经济改革的同时，我们还需要深化政治改革，由于我们的发展在本质上具有广泛性，我们的改革也应该是广泛的。我认为你所提问题的核心是中国民主制度的发展。说到在中国发展民主，我们要在三个方面取得进步：一、我们要逐步改进民主选举制度，让政权真正属于人民、为人民服务。二、我们要改进法制，依法治国，建立独立和公正的司法制度。三、政府应该受人民监督。这要求我们提高政府工作的透明度。政府还有必要接受新闻媒体和其他党派的监督。<br />
<br />
我们需要考虑中国的国情，引入适合中国特色的制度，我们要渐进。<br />
<br />
我很难预测25后将发生什么。我相信，中国的民主制度将继续进步。20到30年后，中国社会将更加民主和美好，法制将得到改进。社会主义将更加成熟和完善。<br />
<br />
<strong>人们说你正在研究日本的制度，因为日本是民主国家，但似乎只有一党能赢得大选。这就是你心目中的中国模式吗？</strong><br />
<br />
世界上有多种民主制度。重要的是它是否真正代表人民的利益。我认为社会主义就是一种民主制度。这种民主制度最根本的是要保证人民有民主选举权、有监督权、有决策权；这种民主制度还应该有助于人民在自由和平等的环境中谋求全面发展；这种民主制度应该建立在完善的法制基础上。&nbsp; &nbsp;<br />
<br />
<strong>你曾说你读过一百遍马可・奥勒留的著作。他是著名的斯多葛派哲学家。他写过这样的话，人不应该沉湎于自我，不应该沉湎于任何自我利益的追逐中，而应该更加关心整个社会。近日我去过中国，我震惊于那里的个人主义、消费主义如此之泛滥。你是否想对中国人民发出信号，让人们少考虑自己多关心社会？</strong><br />
<br />
确实，我多次读过马可・奥勒留的《沉思录》，我被他的文字深深地打动了。我十分珍视道德，我认为企业家、经济学家和政治家都应该重视道德和伦理。在我心目中，衡量伦理和道德的最高标准是公正。当我们思考经济问题时，我们更多地想到公司、资本、市场、技术等等。我们也许忘记了某些方面，比如信仰和道德。只有当我们把这两方面结合起来，我们才能全面破解经济的本源。确实，在中国经济的发展过程中，一些公司以道德为代价追求利益。我们将永远不允许这样的事情发生，因为仅靠这种手段是不能长久的。这就是为什么我们提倡企业道德、职业道德和社会道德。<br />
<br />
<strong>我的最后一个问题是：你肯定一直在关注美国的大选，你对这种奇怪的竞选有什么看法？</strong><br />
<br />
美国的总统选举应该由美国人民作出决定。我密切关注的是大选后中美关系［的走向］。近年来，中美关系发展良好。我们希望无论谁当选为总统，都将继续发展与中国的关系。无论谁当总统，中国都希望继续加强与美国的关系。</div>

							
							
							
							
