																											<div id="postmessage_6548" class="t_msgfont">书名：纳粹德国文学史<br />
作者：J・M里奇<br />
版本：文汇出版社2006年第1版<br />
<br />
&nbsp; &nbsp; 接着九个学生代表分别守住九类图书的书堆，一边往火里扔书，一边说着煽动人心的话：<br />
<br />
　　第一个发言者：反对阶级斗争和唯物主义，捍卫民族共同体和理想主义！我把马克思和考茨基的书付之一炬。<br />
　　第二个发言者：反对堕落和道德败坏，捍卫纪律和家庭、国家的伦理！我把海因里希・曼、恩斯特・格莱瑟和艾里希・卡斯特纳的书付之一炬。<br />
　　第三个发言者：反对政治冷淡和政治背叛，拥护为民族和国家献身！我把和平主义者弗里德里希・威廉姆・福尔斯特的书付之一炬。<br />
　　第四个发言者：反对依据害人的精神分析学夸大无意识冲动的意义，捍卫人类灵魂的尊严！我把西格蒙德・弗洛伊德的书付之一炬。<br />
　　第五个发言者；反对伪造我们的历史，玷污历史伟人，捍卫我们往昔的尊严！我把埃米尔・路德维格和埃纳・黑格曼的书付之一炬。<br />
&nbsp; &nbsp; 第六个发言者：反对犹太人的、民主化的新闻主义，捍卫在国家重建工作中的精诚合作！我把西奥多・沃尔夫和格奥尔格・伯恩哈德的书付之一炬。<br />
&nbsp; &nbsp; 第七个发言者：反对文学出卖第一次世界大战中的士兵，捍卫相互信任的精神对人民的教育！我把艾里希・马里亚・雷马克的书付之一炬。<br />
&nbsp; &nbsp; 第八个发言者：凡对肆意地为我们德国的语言，捍卫对人民最宝贵财富的培育！我把阿尔弗雷德・科尔的书付之一炬。<br />
　　第九个发言者：反对卑鄙无耻和狂妄自大，捍卫对我们不朽的日耳曼民族精神的敬畏和尊重！大火，把图邵尔斯基和奥塞斯基的书吞掉吧！（Ｐ79）<br />
 <br />
