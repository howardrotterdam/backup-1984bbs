													<h2>评杨继绳的《墓碑》</h2>
						
						
																											<div id="postmessage_4376" class="t_msgfont">曾慧燕<br />
<br />
&nbsp; &nbsp;饿死3600万人是什么样的概念？相当于向中国农村投下450枚原子弹，相当于发生150次唐山大地震。第1次世界大战死亡人数只有1000多万人，中国1960年一年就饿死1500万人以上…… <br />
<br />
　　今年是中国「大跃进」50周年和「改革开放」30周年，发生在半世纪前的「大跃进」运动导致的3年大饥荒，是人类历史上空前的大悲剧。在气候正常的年景，没有战争，没有瘟疫，却有几千万个鲜活的生命死于人为的饥饿，出现大规模的「人相食」、人吃人。如今大饥荒过去50年了，这段历史始终含糊不清，几千万亡灵魂归无所。 <br />
<br />
　　新华社退休高级记者、现任《炎黄春秋》杂志社副社长杨继绳，今年5月在香港出版《墓碑－中国六十年代大饥荒纪实》（天地图书公司），此书长达80万字，分上下两册，堪称是血泪交加、资料翔实的长篇调查报告，也是一幅真实记载大饥荒的全景图，详细记述了1960年代大饥荒饿死3600万人的史实，并着重介绍其中12个省饿死人的情况。 <br />
<br />
　　请看书中记载的一段文字；「没有呼天抢地的哭声，没有披麻带孝的礼仪，没有送葬的鞭炮和纸钱，没有同情，没有悲哀，没有眼泪，也没有震惊和恐惧。几千万人就这样无声无息地，精神麻木地消失。有的地方用大车将死人成批地拖到村头的大土坑里，有的地方因无力掩埋，死人的胳膊和腿还露在外面，有的地方死人就倒在寻找食物的路旁，还有不少死人长时间放在家里，被老鼠啃掉了鼻子和眼睛。……」 <br />
<br />
　　「信阳五里店村一个14、15岁的小女孩，将4、5岁的弟弟杀死煮了吃了，因为父母都饿死了，只剩下这两个孩子，女孩饿得不行，就吃弟弟。……」 <br />
<br />
　　饥饿比死亡更恐怖 <br />
<br />
　　此书是作者逐一实地考察、走访大量当事人，包括当年各级干部、饥荒幸存者及见证人，并详细阅读档案资料写成。对没有实地考察的其它省分，他则透过各种渠道查阅了大量的资料，包括公开或未公开的各级档案、会议纪录、谈话纪录及各种地方志、县志，也参阅了许多专着、统计资料等。他穷10年之力，收集了近千万字的资料，写成《墓碑》一书，填补了历史空白。书中涉及的事件、引用的资料，都详细注明来源、出处，有些还配有照片。 <br />
<br />
　　这是一本承载了许多幸存者集体记忆之书，亦是纪念3600多万饿死亡灵的墓志铭。 <br />
<br />
　　1999年秋，杨继绳到信阳淮滨县防胡乡高油坊了解当年情况。70多岁的农民余文海把他领到村外一块麦地边，指着麦地中间的几棵树对他说：那长树的地方原来是一个大坑，上百具尸体就成堆的埋在那里。杨继绳感叹：「如果不是当事人余文海指认，谁也不会知道，在绿油油的麦苗和挺拔的树木下面，深埋着惨烈的悲剧。」 <br />
<br />
　　他在书中描述，死亡前的饥饿比死亡更恐怖。玉米心吃光了，野菜吃光了，树皮吃光了，鸟粪、老鼠、棉絮都用来填肚子。在挖观音土的地方，饥民们一边挖，一边大把大把地往自己嘴里塞着观音土。死人的尸体，外来的饥民，甚至自己的亲人，都成了充饥的食品。 <br />
<br />
　　作为一个有良知的新闻工作者，骨肉亲情是杨继绳写作《墓碑》的动机之一。他的父亲是一位善良、正直的农民，1959年死于饥饿，事隔半世纪，父亲临死时的惨状、家乡惨不忍睹的情况仍历历在目。 <br />
<br />
　　杨继绳说，这本书原打算名为《天堂之路》，后来改为《墓碑》。他指出书名「墓碑」有四重意思：一是为在1959年饿死的父亲立墓碑；二是为3600万饿死的中国人立墓碑；三是为造成大饥荒的制度立墓碑；四是在这本书写到一半时，杨继绳在体检时发现有「病变」（甲胎蛋白呈阳性），「于是我加快了写作的速度，下决心把这本书写成，也算是为自己立一个墓碑。后来有幸在复查时排除了病变，但写此书有很大的政治风险，如因此书而遭不测，也算是为理念而献身，自然也就成了自己的一个墓碑」。 <br />
<br />
　　因此，杨继绳决心为历史作见证，记录这一旷世灾难，以警后世。他强调，把真相公诸于众，不是为了唤起仇恨，而是希望今后不再出现这样的悲剧。大饥荒始终是巨大的历史包袱，迟早要卸掉，并要从中汲取教训。他深刻指出，大饥荒的成因及结果，间接引发了另一场浩劫──文化大革命。 <br />
<br />
　　字字血泪 不忍卒读 <br />
<br />
　　美国西东大学、21世纪中国基金会和中华学人联谊会10月17日至19日在纽约联合举办「中国的历史教训和未来挑战─大跃进50周年和改革开放30周年国际研讨会」，邀请来自中国大陆、香港、台湾、加拿大及全美各地40多名学者与会，旨在探讨中国当代史上大跃进和改革开放这两大历史事件的意义及影响。《墓碑》作者杨继绳也是与会者之一，这次应邀访美，在会上发表「大饥荒期间中国的人口损失」调查报告，并接受《世界周刊》专访。 <br />
<br />
　　「叫墓碑，太沉重」！《墓碑》确是一部令人心情沉重的书，不少人在阅读此书时，难以忍受锥心之痛，几番掩卷，不忍卒读。知名作家倪匡形容，「读《墓碑》，感觉每一个字，都有30个魂萦绕在旁，深深体会什么叫『令人发指』的感觉。」他并形容《墓碑》文字的震撼力，有「字字血泪」之说。 <br />
<br />
　　杨继绳在「墓碑」前言部分，详述了他父亲悲惨死去的一幕。那是1959年4月底，杨继绳在湖北浠水第一中学读高中，他儿时的朋友张志柏匆匆赶到学校通知他：「你父亲饿得不行了，你赶快回去，最好能带点米回去。」他还告诉他：「你父亲没有力气去刨树皮，饿得没办法，想到江家堰去买点盐冲水喝，没想到倒在半路上，是湾里的人把他抬回来的。」<br />
<br />
　　杨继绳当即向老师请假并到食堂停伙3天，取出他的口粮──3斤大米，然后赶回他下湾的家。「走到湾里，发现一切都变了样：门前的榆树（浠水称之为油树）没有皮，白花花的，底下的根也刨光了，剩下一个凌乱的土坑。池塘干了，邻居说是为了捞蚌放干的。蚌有股难闻的腥味，过去是不吃的。没有狗叫，没有鸡跑，连过去欢蹦乱跳的小孩子们也待在家里出不来。湾里一片死寂」。 <br />
<br />
　　他走进家门，发现没有一颗粮食，没有一点能吃的东西，水缸里连水也没有。「饿得走不动，哪有力气挑水啊！父亲半躺在床上，两眼深陷无神，脸上没有一点肌肉，皱纹宽阔而松弛。他想伸出手招呼我，但没有伸起来，只是动了动。这只手和上生物解剖课时看到的人体骨骼标本上的手差不多，外面虽然有一层干枯的皮，但没有遮住骨骼上每一处的凸起和凹陷！看到这只手，我心里陡起一陈酸楚和震撼：原来通常说的『瘦得皮包骨』是这样的恐怖和残忍！他嘴里嘟啷着，声音很低，他是叫我赶快走，赶快回学校去。」 <br />
<br />
　　父亲变成这个样子，杨继绳心里充满了懊悔和自责，「我为什么不早点回来挖野菜呢，为什么不早点请假拿点米回来呢？」他用带回的米煮成稀饭，送到床边，然而父亲已经不能下咽了，三天以后与世长辞。 <br />
<br />
　　埋葬了父亲，他很悲痛，但没有丝毫埋怨政府。「我不认为这和政府有什么关系，也不认为这和『三面红旗』（指总路线、大跃进、人民公社）有什么关系。我对当时宣传的大跃进的成就、人民公社的优越性依旧深信不疑。我不知道更远地方发生的事情，我以为我家乡发生的事是个别现象。我以为父亲的死只是我一个家庭的不幸。想到伟大的共产主义即将到来，家庭的这点不幸算什么？党教导我遇事要牺牲『小我』，维护『大我』，我绝对听党的话。这种认识一直保持到文化大革命时期。」 <br />
<br />
　　杨继绳说，那时，他没有怀疑过共产党和政府，所有被灌输的东西都全盘接受。「1957年反右派时，党组织说右派分子坏，我就认为坏。1958年大跃进，我也是学校里的积极分子，我写的赞美大跃进的诗曾送到黄冈地区教育展览馆。当时我是团委宣传部长，兼任学校《青年共产主义者》油印小报的主编。白天参加劳动，晚上编报纸。1959年元旦，我为这张报纸写《元旦献词》，热情地歌颂『大跃进』。在全校庆祝新年的大会上，校长王占崧一字不差地宣读我写的这篇文章，作为向全校师生的献词。」 <br />
<br />
　　杨继绳说他不怀疑「三面红旗」，除了无知以外，还有整个社会强大的政治压力，使他不敢怀疑。「不停顿的革命大批判，耳闻目睹的严酷惩罚，使人们产生了一种恐惧心理。这种恐惧不是看到毒蛇猛兽那种陡然产生、陡然消失的恐惧，而是化解在神经和血液中，成为每个人的生存本能。人们像避开炽热火焰一样而避开政治危险」。 <br />
<br />
　　1960年他考入北京清华大学，大学期间还是很听话，一直担任团支部书记，1964年5月加入中国共产党，使他认识最初发生转变的是文化大革命。1966年8月到12月，他和几名同班同学一起到20多个城市「串联」，各地大字报揭发出高官的腐败和特权。「我开始不迷信权威，不迷信高官，也不迷信报纸上所说的一切。我开始怀疑中国共产党多年来向我灌输的神话。我和多数普通群众一样，是以反对高官的特权的心态参加文化大革命的。在文化大革命中，湖北省长张体学说的一句话使我震惊：湖北省在3年困难时期饿死了30万人！这时我才知道，发生在我家庭的悲剧不是个别现象。」 <br />
<br />
　　杨继绳在清华大学拖拉机制造专业毕业后，分配到新华社。新华社记者可以接触到其它人无法接触到的社会层面。他不仅知道了很多与党史教科书上不一致的真实情况，也看到了城市工人的贫困及许多与生活。「作为新华社记者，我更知道报纸上的『新闻』是怎样制造出来的，知道新闻机构怎样成为政治权力的『喉舌』。」 <br />
<br />
　　改革开放以后，中国的思想禁锢比过去松动了不少，一些真实的历史开始透露出来。杨继绳说：「过去党教导我们，在抗日战争期间，只有共产党抗日，国民党一味投降妥协；现在才知道，国民党支撑着抗日的主战场，有一、两百位将军为国捐躯。过去党教导我们，由于自然灾害，少数地方发生了饥荒；现在才知道，完全是由于人祸，几千万人饥饿而死……我开始知道，中国共产党党史，甚至近百年的中国历史，都是按照共产党的需要，进行了歪曲和编造。」 <br />
<br />
　　还原历史 追求真实 <br />
<br />
　　一旦知道自己过去长期受到蒙骗，就产生出一种摆脱蒙骗的强大力量。当权者越是掩盖真实，就促使他更加追求真实。他不仅大量阅读新发表的史料，也在采访新闻中努力了解真实的过去。「我亲身经历的1989年北京风波（6四事件），更使我大彻大悟。年轻学生的鲜血，把我的头脑里过去几十年接受的种种谎言洗刷一尽。作为新闻记者，我力求发表真实的报导和言论；作为学者，我有责任还历史的本来面目，并把真实历史告诉受蒙骗的更多人。」<br />
<br />
　　在摆脱蒙骗和追求真实的努力中，杨继绳一步一步地弄清楚了他父亲死去的社会背景。虽然时间过去了几十年，他对父亲死因的思考却日益深入。 <br />
<br />
　　当他得知，父亲因大饥荒饿死的年分，中国并没有什么天灾，是一些正常的年分，虽谈不上丰收也谈不上灾害。当他得知，河南省发生人吃人的惨剧时，信阳100万人死于饥饿时，河南至少有25亿斤粮食库存，临近的湖北省至少有13亿斤粮食库存，仅动用这两省库存，根本不会饿死人。明知道大面积饿死人，毛泽东还大幅增加当年全国征粮库存额度，他的愤怒无以复加。大饥荒完全是人祸，原因是谎言，加上人民公社对人身控制、计画经济的荒谬以及极权政权的残酷。 <br />
<br />
　　当一个人从当年迷信的谎言中醒来时，首先是愤怒，然后陷入对历史真相的沉思。杨继绳说，发生在他家的悲剧，同时发生在全国上千万个家庭中。 <br />
<br />
　　在新华社的第1个10年，他跟潮流写了许多稿子，以上人民日报头版为荣，有一年曾在人民日报发了14个头版头条。现在回顾，杨继绳说90%的稿子都应当烧掉。第1个10年，他只有两篇稿子是凭良知写的：一篇是《天津驻军大量占用民房，严重影响军民关系》，经中央批评军队全部退出民房；另一篇是《天津劳动生产率调查》。 <br />
<br />
　　第2个10年，杨继绳尽可能说真话，坚持不说假话，但有时还不得不跟一下潮流。 <br />
<br />
　　第3个10年，杨继绳决心不跟潮流，只写自己想说的真话。「说真话，求真理，做真人」这是他的追求。 <br />
<br />
　　他说：「在权力面前说真话。」「一无所求，二无所惧，就能自立于天地之间。」 <br />
<br />
　　父亲墓碑 立在心中 <br />
<br />
　　杨继绳有感于大饥荒已经过去50年，在中国国内还没有一本记述这个历史事件的书。「这不仅是历史的遗憾，也对不起几千万饿死的冤魂」。 <br />
<br />
　　面对大饥荒造成的严重后果，刘少奇曾对毛泽东说：「饿死这么多人，历史要写上你我的，人相食，要上书的！？」刘少奇还说过，对这场灾难要刻上碑，记上帐，「要子子孙孙地传下去，以后再也不犯这个错误。」1962年春，刘在与邓力群谈话时也讲到「历史上饿死人的事是要写到史书上去的」。 <br />
<br />
　　从20世纪90年代初开始，杨继绳利用到全国各地采访的机会，查阅有关资料，访谈经历过大饥荒的人。他从大西北到大西南，从华北到华东，从东北到华南，查阅了十几个省的资料，访谈了上百位当事人。经过10年努力，收集了上千万字的资料，记下了10多本当事人谈话记录。他「终于比较全面和比较深入地得到了这场持续3、4年的大饥荒的真实情况」。 <br />
<br />
　　1980年代，杨继绳家乡兴起为先人修建墓碑的风气，特别是在外面当大官的人，修的墓碑更气派。亲戚朋友也劝他为父亲修建个墓碑。 <br />
<br />
　　杨继绳想，「我虽然没有当什么大官，我为父亲立的墓碑一定要比当大官的更气派。然而，我想到了1959年家乡那些墓碑的命运，有的被拆来修建水利设施，有的在大炼钢铁中用来做土高炉的底座，有的则铺在路上任千人踩、万人踏。墓碑越气派，被拆除的危险就越大。父亲墓碑是不能不修的，但是不能修在大地上，必须立在心里；立在心里的墓碑，不会遭人践踏，也不会被人拆除。」 <br />
<br />
　　接着，他花了10年功夫，调查大饥荒真相。每到一地，他以正面报导为幌子，要求地方党委开放大饥荒的档案，抄写资料。多年如一日，他白天完成工作，晚上除了特殊情况，如生病、家中有事，他都在写作，终于完成《墓碑》。「这本书也算是几千万饥饿亡灵的一座墓碑，我希望饥饿的亡灵得到安慰。」 <br />
<br />
　　他在心中为父亲修建了一座墓碑。《墓碑》这本书，就是铭刻在他心中墓碑的文字表达。「即使我在这个世界消失了，这个文字表达的心声，将存留在世界各地的一些大图书馆中。……」 <br />
<br />
　　他认为，如此重大的历史事件，应当立上巨碑，以警后世。「我想，为了牢记历史教训，仅有我这本书是不够的。唐山大地震有了纪念碑，日本广岛、长崎有了纪念碑，欧洲很多地方建立了二战纪念碑。中国也应在一切饿死人密集的县分（如信阳，通渭，罗定，亳州，凤阳，遵义，金沙，郫县，荣县，丰都，大邑，馆陶，济宁等等），在饿死人最多的四川、安徽、贵州、河南、山东、甘肃、青海等省的省会城市，以及在天安门广场，建立大饥荒纪念碑。这些纪念碑不仅纪念亡灵，也是让人们永远记住这场灾难，从中吸取教训，让悲剧不再重演。」 <br />
<br />
　　他强调，墓碑是凝固的记忆。人类的记忆是国家和民族赖以进步的阶梯，是人类航程前进的路标。「我们不仅要记住美好，也要记住罪恶，不仅要记住光明，也要记住黑暗。极权制度下的当权者隐恶扬善，文过饰非，强制地抹去人们对人祸、对黑暗、对罪恶的记忆。因此，中国人常犯历史健忘症，这是权力强制造成的健忘症。我立的这块墓碑恰恰是让人们记住人祸、黑暗和罪恶，是为了今后远离人祸、黑暗和罪恶。」 <br />
<br />
　　内情惊人 力透纸背 <br />
<br />
　　《墓碑》面世时间不长，引起海内外读者极大关注，佳评如潮。中国大陆自由作家、历史学者丁东说：「这是一部力透纸背的信史。」 <br />
<br />
　　中国社会科学院哲学所研究员徐友渔指出，虽然中西方作者以前出版了多种有关大饥荒的书籍，但「就资料的全面和翔实，内情披露的惊人，论证的理性和严密，以及结论的权威可信而言，这本书是前所未有的」。 <br />
<br />
　　他指出，《墓碑》一书全面、真实地记载了全国大饥荒的情况，书中详载各省饥荒的惨烈实况和剖析饥荒发生原因，有力驳斥官方谎言，并披露大量内幕，例如，1961年，粮食部和国家统计局负责人受命让各省填写一个有关粮食和人口变动的统计表，汇集后得知全国人口减少了几千万！这份材料只报周恩来和毛泽东两人，周恩来阅后命令立即销毁，不得外传。事后周还追问是否销毁了，在得到肯定答复后才放心。 <br />
<br />
　　徐友渔认为，「这是一部可以和诺贝尔文学奖获得者索忍尼辛(又译索尔仁尼琴)的《古拉格群岛》相媲美的巨著。虽然，关于中国的古拉格，也有中国人作了深刻的揭露。如果说古拉格是史达林时代的代名词，那么，饿死几千万人则是毛泽东时代的标志」。 <br />
<br />
　　他说，杨继绳曾惊叹索忍尼辛为记录罪恶而收集证据的专注与坚韧，为中国历次政治运动的受害者和幸存者未能专心致志还原历史而叹息。而《墓碑》一书既体现了索忍尼辛式的良知、民族责任感和历史洞察力，还在史料的汇集、分析和内情披露方面大大超出《古拉格群岛》。「当然，可以说杨继绳的身分和条件优于索忍尼辛，但一般的规律是，身分和条件越有利，包袱就越重，顾虑就越大。像杨继绳这样出身于体制内而为正义冒险犯难、孤注一掷，虽不是绝无仅有，也只有顾准、李慎之等少数前例」。 <br />
<br />
　　徐友渔指出，在大饥荒年代发生的许多事，今天看来匪夷所思，有些看起来是笑话，但着实令人辛酸。1960年春，甘肃定西地区农村断粮，地委书记却说：「宁饿死人，也不能向国家要粮食。」在搜刮粮食的过程中，通渭县向公社下的命令是：「完不成粮食任务，提头来见！」当时的口号是：「宁欠血债，不欠粮食。完成粮食任务就是血的斗争。」 <br />
<br />
　　1960年冬，河北省委决定农民口粮为每天4两（旧制，相当于现在的2两半），省委负责人说：吃4两的是马列主义，超过4两的是非马列主义。随后，省委提出「吃4两，晒太阳」的口号。徐友渔说，人不能进行光合作用，晒太阳怎能顶得上吃粮食？ <br />
<br />
　　千村薜荔 万户萧疏 <br />
<br />
　　提到中国的大饥荒，人民首先想到的是河南信阳，这是中国大饥荒饿死人最多的地方。 <br />
<br />
　　当时在四川、河南 、甘肃、安徽省不少地方，包括信阳、通渭、凤阳、亳州、罗定、无为、馆陶及济宁等，几乎家家都有人饿死，很多家庭人口全部死绝，有的村庄死得绝无人迹。正是应了毛泽东在1958年6月《送瘟神》里的诗句：「千村薜荔人遗矢，万户萧疏鬼唱歌。」<br />
<br />
　　《墓碑》详细记录了以信阳地区为重点，下至县、乡、村，上至省委、中南局和中央的情况，比对、分析了各种资料和方法之后，得出结论说，河南饿死人至少为300万，信阳一个地区饿死人100万，占该地区总人口的八分之一以上。 <br />
<br />
　　甘肃总人口只有1300万，全省饿死人数在100万以上。死人比率最高的在通渭县，将近三分之一，70%的家庭有死人，有的全家死绝。当时被派遣去解决「通渭问题」的一个干部回忆说，他们只看到炕上、路上、田间、地头都躺着人，死人活人不分，活人没有力气或顾不上埋葬死人，他们不怕死人怕活人，因为活人想吃人。 <br />
<br />
　　四川历来号称「天府之国」，土地肥沃、物产丰富，但在大饥荒年间死人最多，达到1000万至1200万，之所以难于准确估计数字，是因为四川省委在当时就从政治上控制人口统计工作，命令上报死亡数字多的地区「重新调查」。 <br />
<br />
　　四川是全国饥饿时间最长的省分，因其最高领导人李井泉善于揣摩毛泽东的心意，对于极左跃进的做法坚持不降温，并大量外调粮食，以证明政策正确，形势大好，他以千万人的生命为代价，晋升为中央政治局委员。 <br />
<br />
　　安徽凤阳以农民逃荒要饭闻名，这个不到40万人的县，3年大饥荒饿死9万人，将近四分之一。饥荒的主要原因是高指标、高估产、高征购，凤阳县委1958年上报指标8亿斤，实收1亿6000斤，国家强力征购占43%，造成农民缺粮断炊，1959年产量为1亿8000斤，上报为4亿斤。安徽全省饿死的人在500万到600万之间，超过总人口的15%。在安徽亳县，由于吃人肉的情况不断发生，出现了人肉市场交易，农民摆摊设点或流动串乡卖人肉。 <br />
<br />
　　3年大饥荒到底饿死了多少人？这是一个人们一直关心，引起激烈争论和众说纷纭的问题。 <br />
<br />
　　在该书第22章，杨继绳参照中外多方面资料、比较分析了各种方法后的结论是：在1958年到1962年饥荒期间，全国非正常死亡人数大约3600万；另外，因饥饿使得出生率降低，应出生而没有出生的人数大约4000万，大饥荒使中国人口损失大约7600万。徐友渔认为，这是一个比较科学、可信的数字。 <br />
<br />
　　美国明尼苏达诺曼学院教授丁抒10月17日在「中国的历史教训和未来挑战国际研讨会」上，发表《大跃进、大饥荒期间非正常死亡人数的下限》的演讲时指出，1960年前后，整个中国有如一个巨大的冰窖。千千万万个村庄，每天都有许多农民在饥饿中死去，在人类历史上，任何一场人为的战祸或天灾、瘟疫，都不曾在这么短的时间内夺去那么多人的生命。 <br />
<br />
　　丁抒说，全国究竟有多少人死亡？本来无需别人开会来讨论。<br />
<br />
　　可是中国国家统计局直到现在还用大大低于实际数字的1700万人「唬弄世人」，以至他们不得不在50年后的今天，用事实「揭破其谬误」。他引用大量资料，得出「全国在大跃进／大饥荒期间的非正常死亡的下限是3500万」。在《墓碑》之前，不断有中外研究者综合各种材料和资料，估算出饿死的总人数，大致说来，在2000万到4000万之间。而新老左派则攻击这些资料，称这些资料偏高，是对毛泽东本人、大跃进和人民公社的丑化。 <br />
<br />
　　徐友渔对此反驳说：「我们可以取最低数的十分之一，质问左派们：难道你们认为，饿死200万人就天经地义？」 <br />
<br />
　　饿死3600万人是一个什么样的概念？杨继绳说，这个数字相当于1954年8月9日投向长崎的原子弹杀死人数的450倍。1945年8月6日，美国飞机向广岛投下的原子弹炸死7万1000人；8月9日，美国飞机向长崎投下了另一颗原子弹，炸死80万人；即大饥荒相当于向中国农村投下了450枚原子弹。 <br />
<br />
　　这个数字相当于1976年7月28日唐山大地震死亡人数24万人的150倍，也可以说大饥荒相当于发生了150次唐山大地震。 <br />
<br />
　　这个数字超过了第1次世界大战的死亡数字。第1次世界大战死亡人数只有1000多万人，中国1960年1年就饿死1500万人以上。 <br />
<br />
　　大饥荒的惨烈程度远远超过了第2次世界大战。第2次世界大战死亡人数4000万到5000万之间。中国这3600万人是在3、4年间死亡的，多数地区死人集中在半年之内发生。 <br />
<br />
　　易子而食 抢吃人肉 <br />
<br />
　　中国古籍记载「易子而食」的故事，而在1950、60的大饥荒年代，吃亲生儿女的事件就有多起，「人相食」并非个别现象。在河南信阳的一些乡镇，在甘肃通渭，杨继绳都听到当事人讲述骇人听闻的吃人故事，他还见到吃过人肉的人，听他讲述过人肉的味道。据亲历庐山会议和大跃进的李锐说，当时人吃人的纪录全国至少上千起。这些悲剧，在《墓碑》各章节里，有详尽记载。 <br />
<br />
　　在当时及以后的几十年中，中国的一切书刊、报纸及官方档案，都极力回避和掩盖这一大规模的人类悲剧。各级干部对饿死人的事也是三缄其口。对饿死人数的统计，各级干部弄虚作假，千方百计缩小死亡数字。为了永远掩盖这一事实，当局还下令销毁各省报上来的人口减少数千万的资料。 <br />
<br />
　　公共食堂 大吃大喝 <br />
<br />
　　为什么会发生全国性的大饥荒，为什么会饿死这么多人？《墓碑》一书披露下述原因： <br />
<br />
　　由于大跃进的浮夸，全国各地都搞高指标、高估产、高征购。1958年宣布粮食总产量为7500亿斤，以后统计的数字（还有虚夸）为4000亿斤，实际产量基本与上年持平，但征购数量增加了21%，其结果就是从农民的口粮中硬挖硬挤。 <br />
<br />
　　要完成高征购指标，农民必须把口粮、饲料、种子搭进去，在完不成征购任务时，上级部门认为农民在搞瞒产私分。各地普遍用对基层干部撤职、开斗争会、毒打、劳教、逮捕等方式强迫交粮，被打伤、打死的干部和农民不计其数。斗争会上有这样的问答：「生产队大跃进了没有？」「大跃进了。」「人民公社优越不？」「优越。」「那你为什么不交粮食？」 <br />
<br />
　　还有这样的口号：「谁藏一粒粮食，就等于私藏一粒子弹。」著名科学家钱学森发表文章证明，每亩产量可以达到3万9000斤，这为反瞒产私分提供了「科学论证」。 <br />
<br />
　　「墓碑」一书以大量事实指出，公共食堂制度是大量饿死人的重要的祸因。兴办公共食堂的过程是消灭家庭的过程，也是对农民掠夺的过程。农民家庭的灶拆了，锅盆碗盏和桌椅板凳被公共食堂征用。粮食和柴草集中到食堂，家畜、家禽也集中到食堂饲养。社员挖的野菜也要交给食堂。有的地方，除了公共食堂以外，家庭的烟囱不允许冒烟。 <br />
<br />
　　公共食堂最先暴露出来的危害就是浪费粮食。食堂初开办的最初两个多月，各地放开肚皮大吃大喝。不管劳动好坏，都一样吃饱喝足。 <br />
<br />
　　公共食堂最重要的效能是把「无产阶级专政」贯彻到每一个人的肚子里。办了公共食堂以后，生产队长是一「堂」之长，谁不听话，他就不让谁吃饭。办公共食堂，实际是让农民把饭勺子交到了领导人手里，也就是把生存权交到了领导人手里。农民失去了饭勺，就失去了生存权。此书记载了大量的各地因「扣饭」而把人活活饿死的事实。 <br />
<br />
　　杨继绳说，人民公社是农业集体制度的发展，也是极权制度的进一步发展。人民公社刚成立时，普遍实行供给制和工资相结合的分配制度。供给制，实际上是由各级官员对老百姓的生活必须品进行「供给」，这不仅为各级官员控制公社财富创造了条件，也从基本生活上控制社员。官员不「供给」，社员就不能生存。由于政府不具备供给能力，供给制只是昙花一现。供给制虽然只持续了很短时间，却造成了极大的浪费，特别是粮食的浪费。 <br />
<br />
　　在几千万冤魂中，有一部分是被基层干部被活活打死或逼死的。河南省委书记处书记杨蔚屏在1960年10月15日《关于信阳事件的报告》中提供了这样的数字：仅光山和潢川两个县被打致死就有2104人，被打致残的仅潢川一县就有254人。其中被打死或致残的不只是农民，也有不听话的基层干部。在农村，所谓无产阶级专政，实际是干部专政，谁的官大，谁就可以对他所管辖的臣民和下级任意欺凌。 <br />
<br />
　　在公社化、反「瞒产私分」、办公共食堂的过程中，有抵触情绪的农民，因饥饿偷吃了集体青苗的农民，在水利工地上因饥饿干活不力的农民，都可能遭到残酷的处罚。处罚农民的刑罚有几十种：吊打、罚跪、游街、扣饭、冷冻、晒太阳、割耳朵、「炒豆子」等。惨不忍睹。信阳地区罗山县彭新公社有17名预备党员，在「反瞒产」中打了人的16人，都「光荣地」转为正式党员，只剩一个不得转正，因为他没打人。在这本书各章节中，记录了大量打人致死的惨案。需要指出的是，这些惨案是1961年整风整社中揭露出来的，多发生在「三类队」（即落后队），不是每一个生产队都发生这样惨烈的事情。不过，根据毛泽东的说法，「三类队」也占生产队总数的三分之一，在三分之一的生产队里发生这样的惨案，也是相当普遍的。 <br />
<br />
　　上天无路 入地无门 <br />
<br />
　　中共各级地方政府为了完成征购任务，在农村以开展「两条道路斗争」的方式大搞「反瞒产私分」运动，用政治压力、精神折磨和残酷的暴力，搜刮农民仅余的口粮和种籽，农民稍有不满就要挨打，面临种种刑罚，甚至被活活打死。 <br />
<br />
　　乔培华根据中共档案撰写的《信阳事件》调查报告，记录了数十宗光山县槐店公社大树大队发生的血淋淋事例，信手拈来几例： <br />
<br />
　　1959年10月15日，熊湾小队张芝荣交不出粮食，被捆绑后用劈柴、木棒毒打后死亡，大队干部还用火钳在死者的肛门里捅进大米、黄豆，一边捅一边骂：「要叫你身上长出粮食来！」张被打死后，留下8岁、10岁两个小孩先后饿死； <br />
<br />
　　1959年10月15日，陈湾小队社员陈小家及儿子陈贵厚因交不出粮食，被吊在食堂的房梁上毒打，后又扔到门外用冷水冻，陈家父子7天内先后死亡，家里留下的两个小孩也活活饿死； <br />
<br />
　　1959年11月8日，熊湾小队社员徐传正被诬陷「有种不交」，被吊在食堂房梁上，残酷毒打，6天后死亡。徐一家6口随后全部饿死； <br />
<br />
　　1959年11月8日，晏湾小队社员钟行简因被认为「违抗领导」，被干部用斧头砍死； <br />
<br />
　　1959年11月13日，熊湾小队队长冯首祥因没有向来这里的大队干部让饭，被看成瞧不起大队干部，就被吊在大队食堂房梁上毒打，并将其耳朵撕掉，6天后死亡； <br />
<br />
　　1959年12月9日，陈湾小队社员陈富厚因无粮可交，被绳子穿耳，并捆绑吊在梁上用扁担打、冷水淋，当场死亡。为防止其子陈文胜（17岁）声张，诬陷他宰杀耕牛，捆绑起来毒打致死…… <br />
<br />
　　光山县县委书记刘文彩到槐店公社主持「反瞒产」运动，连续拷打40多个农民，打死4人。该公社一级干部中，亲自主持和动手打人者占93%。河南省委委书记处书记李立在1960年11月28日向吴芝圃的报告中，提供了更重要的情况，摘抄如下： <br />
<br />
　　光山县槐店人民公社1959年秋灾，全社平均亩产172斤，总产1191万斤，公社党委报亩产626斤，总产4610万斤，县里分配征购任务是1200万斤，超过了全公社的实际总产量。为了完成征购任务，他们不惜采取一切手段，大搞反瞒产，把群众仅有的一点口粮搜刮一空。食堂普遍停伙，死人现象相继发生。刘文彩和公社党委把食堂停伙死人归于富裕中农进攻、阶级敌人破坏，归结为两条路线的斗争在粮食问题上的表现。反瞒产斗争持续8个月之久，60、70天内粒米全无，造成人口大量死亡。 <br />
<br />
　　被打的有3528人，当场打死群众558人，打后致死的636人，致残的141人，迫死14人，打跑43人。 <br />
<br />
　　除了毒打、脚}、冻、饿外，还采取了冷水浇头、拔头发、割耳朵、竹签子穿手心、松针刷牙、点天灯、火炭塞嘴、火烙奶头、拔阴毛、通阴道、活埋等数十种极为残忍的酷刑。 <br />
<br />
　　原公社党委书记江某等指使炊事员把13个到公社要饭的小孩拖到深山，全部活活冻饿而死。 <br />
<br />
　　广大群众处于上天无路、入地无门的绝境，骨肉不得相顾。妻离子散，家破人亡，遗弃子女，抛尸路旁。全公社有381人因饥饿难忍破坏尸体134具（作者注：这里说的「破坏尸体」就是从尸体上割肉回家吃 ）。…… <br />
<br />
　　在正常情况下，如果遇到饥荒，或者得到外界的救助，或者外出逃荒。但是，在当时的制度下，农民没有求助和外出逃荒的权利。各级政府千方百计地对外封锁饥饿的消息。公安局控制了所有的邮局，向外面发出的信件一律扣留。中共信阳地委让邮局扣了1万2000多封向外求助的信。为了不让外出逃荒的饥民走漏消息，在村口封锁，不准外逃。对已经外逃的饥民则以「盲流」的罪名游街、拷打或其它惩罚。 <br />
<br />
　　根据毛泽东的喜好和宣导大办公共食堂，是造成农民饿饭和死亡的重大原因。此书作者根据资料分析认为，如果没有公共食堂，饿死的人数可能减少三分之一，在有些地方可能减少一半。大办公共食堂的过程，就是前两个月吃饭不要钱，大吃大喝，然后闹饥荒的过程，就是强迫农民入伙，统一用粮，没收锅碗瓢盆，谁家冒炊烟抓谁，使农民家徒四壁、一无所有。 <br />
<br />
　　公共食堂的最大危害是「把无产阶级专政贯彻落实到每一个人的肚皮」，干部掌握了让谁吃饭和不让谁吃饭的权力，同时得到多吃多占，搞特殊化的基地。 <br />
<br />
　　政府和干部滥用权力使饿得快死的农民雪上加霜，他们派民兵围、追、堵、截，设卡抓捕，不让农民逃荒，断了饥民的最后一条求生之路。 <br />
<br />
　　中共共领导面对自己一手制造的大惨剧，编造出大饥荒是出于「三年自然灾害」和「苏联修正主义逼债」的谎言，把罪责推给老天爷和苏联，长期以来，「三年自然灾害」和「苏修逼债」成了中国人回顾大饥荒年代的口头禅。《墓碑》对此作了详细的考证和有力的批驳。<br />
<br />
　　杨继绳引证权威的气象资料和权威的气象学家的观点，说明1959至1961年是正常年景。中国地域辽阔，每年发生天灾在所难免，但这3年的自然灾害并不比其它年分严重。 <br />
<br />
　　他进一步指出，历史资料表明，粮食产量和天灾之间并不存在正比相关关系，在大饥荒期间，中国政府以巨额资金和物资援助一些国家，其中许多国家的生活水平大大高于中国。在农民大量饿死的同时，大量粮食被用于出口，1959年与1957年相比粮食减产，但出口量增加一倍以上，创造粮食出口历史最高纪录，这个数量够2450万人吃一年。 <br />
<br />
　　1960年，在大批农民饿死之际，当局不仅没有考虑开仓放粮，反而刻意继续增加国家粮食库存，这一年饿死人最多，而国家尚有几百亿斤粮食库存。 <br />
<br />
　　当毛泽东得知信阳发生饿死人的严重事件时，他说事件的性质是阶级敌人窜夺了领导权，是反革命复辟，对劳动人民实行疯狂的阶级报复，解决办法是实行民主革命补课，这导致对基层干部大规模的斗争、撤职、逮捕。 <br />
<br />
　　有人曾辩解说，毛泽东在3年困难时期也和全国人民同甘共苦，不吃肉。此书引证官方出版物说明，毛的一份西餐菜谱记载了各式风味的鱼虾共有17种。另一位知情人说，医生鉴于猪肉胆固醇高，建议毛不吃猪肉改吃牛羊肉。在此期间，各地为毛和一些政治局常委修建的别墅、宾馆豪华程度超过国宾馆。 <br />
<br />
　　十分人祸 无关天灾 <br />
<br />
　　前全国人大代表香港代表团团长吴康民高度评价杨继绳及《墓碑》。说他是一位治学严肃、搜集资料丰富，而又思想开放、大胆敢言的作家；《墓碑》是继杨继绳此前出版的《中国改革年代的政治斗争》（内容涉及访问赵紫阳的敏感题材）、《中国社会各阶层分析》等著作后，「对中国现代政治史的一大贡献」。 <br />
<br />
　　吴康民指出，杨继绳的「墓碑」，触及大规模的「人祸」，揭露了极大的「黑暗面」，书中强调四点，一、中国大饥荒的真相长期不为人知，大多以三分天灾七分人祸搪塞民众，其实完全是十分人祸，没有天灾。 <br />
<br />
　　二、一般人们知道河南省当年饿死了300万，却不知道中国死人最多的省分是「天府之国」四川，保守估计为1000万人。原因是四川省委书记李井泉跟毛最紧，四川揭盖子最晚。大饥荒与反右打压敢言者有直接因果关系，大饥荒是文革的重要起因。 <br />
<br />
　　三、人吃人不是特别的例子，而是普遍存在，有明确纪录的已达3000多例，其中纪录有一次政府把吃人者与煮熟的人肉拿到会场批斗，结果群众饥饿难忍，一人说：「我也尝尝！」众人蜂拥齐上，批斗大会变成抢人肉、吃人肉大会。 <br />
<br />
　　四、中国大饥荒的影响超过文革，其死亡人数相当于全世界二次大战死亡人数的总和，当然也远远超过8年抗战中国死亡人数，大饥荒死亡数相当于向中国农村投了450颗广岛原子弹。 <br />
<br />
　　英国《金融时报》专栏作家、美国哈佛大学博士丁学良称此书为「你不能不看的墓碑」、「一本值得尊敬的书」。他说：「我从来没有写过任何书评，因为没有时间。可今天，在回顾中国改革发起30年的时候，我却要破例写一次，因为《墓碑》太有价值、太有意义了…。」 <br />
<br />
　　丁学良指出，此书在香港一上市，西方的好几家大报刊即用严肃书评予以反应，对它评价极高。他也认为这是他20年来所读到的研究「大跃进」著述「最全面、最实证、有最多第一手资料和最多细节的一部杰作」。尽管他一直关注这方面的资料，但这部书「依然让我吃惊─有些细节我都看不下去，时常要放下来，喘一口气，因为里面的内容太沉重了」。 <br />
<br />
　　杨继绳表示，这本书题材沉重，估计出版后会让他面对更大的政治压力和风险，但是追寻真相的使命、反省历史的责任，让他「必须给自己一个交代」，也为父辈和那个时代立起一座文字的、也是更永久的墓碑。 <br />
<br />
　　「我坚信，在中国，总有一天极权制度会被民主制度取代。这不是很遥远的事情。……在极权制度彻底死亡之前，我提前为它立了个墓碑，让后人知道：人类社会在历史的某一阶段、在某些国度，曾经有一种以『解放全人类』的名义建立的、实际是奴役人类的制度。这个制度宣扬并实践的『天堂之路』，实际是死亡之路。」 <br />
 <br />
来源：世界周刊</div>

							
							
							
							
