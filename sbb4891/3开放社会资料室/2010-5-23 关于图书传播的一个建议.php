																											<div id="postmessage_633607" class="t_msgfont">当我们传播图书的时候，大多利用的是网盘。<br />
网盘的问题:<br />
1，国外的网盘，广告，时效，流量限制。<br />
2，国内的盘，违禁的基本被删。<br />
3，国外的网盘一旦被屏蔽，那些只会用普通代理的网民下载就非常不便。<br />
解决方法：<br />
谷歌及国外的在线文档。<br />
1，先说谷歌的<br />
Google 文档接受最常见的文件格式，包括 DOC、XLS、ODT、ODS、RTF、CSV 和 PPT 等。<br />
2,谷歌支持的格式非常多，而且如果你喜欢某一篇在线文档，可以输出为PDF，Word下载到本地。上次aiww的文章我就是这个做的。<br />
这样的优势是什么：<br />
1， 可以在线看，大方便。相对于一些PDF还要下载PDF阅读器，在线看方便多了。即使被墙了，直接代理过去就能看。<br />
2，喜欢的可以输出到本地，不用再传到什么网盘（据说谷歌文档有流量限制，未知）<br />
3，风险低，如果有一天你电脑被查了，搜到一大批禁书，会有麻烦，而如果你把所有禁书都存在线文档上面，就没这个顾虑了。<br />
就说这么多。</div>

							
							
							
							
