　　战俘营的居住环境和饮食条件急速恶化。科泽利斯克营内的许多建筑物“呈半损毁状态，窗户上没有玻璃，房顶漏水，连通铺都无法架设 ”，就是这样的住房也只能安排一半战俘，另一部分战俘或睡在夏季帐篷，或被安置在猪圈。尤赫诺夫营的许多战俘不得不在3℃～7℃的气温下，“住在凉台上、马厩里、窝棚里”，甚至“许多人连一个坐的地方都很难找到”。<br />
<br />
　　随着军事行动的结束，如何处置大量波兰战俘需要苏联领导人立即作出决定。10月1日，一个由中央书记日丹诺夫牵头的委员会讨论了战俘问题。之后他们向斯大林建议对战俘进行分类处理，方案把战俘分成六类，并作出不同处理：对居住在西乌克兰、西白俄罗斯的乌克兰族、白俄罗斯族及其他民族的士兵战俘，予以遣散回家；留下25000名战俘修筑公路；故乡被划归德国的波兰领土上的士兵战俘暂时单独关押，待与德国人谈判解决了他们的遣送问题后再送其回家；为被俘军官建立单独战俘营，中校到将军的军官以及军政要员与其他军官分开关押；间谍人员、反间谍人员、宪兵、狱吏和警察单独关押等。随后两天之内，联共（布）中央政治局和苏联人民委员会做出了相关决议。<br />
<br />
　　政治局和人民委员会通过的决议中虽然没有道明是本着“阶级原则”对战俘进行分类的，但这一原则通过军衔上的区分得到了反映。在苏联的体制下，对特定社会群体进行分类和对人们的政治属性予以界定，往往首选使用“阶级原则”。在决定释放哪些战俘和不释放哪些战俘时，对苏联领导人更有意义的，不是军衔上的差别，而是阶级差别。在苏联领导人看来，继续关押那些“被征召来的农民”是不适宜的。到11月中旬，已有42400士兵战俘被遣返回家，他们成了一种特定的政治思维下的“幸运儿”。相反，那些被留下的军官和军政要员最终成了日后悲剧的主角。由此可见，对波兰战俘的最初处理，受到了苏联特定的社会政治观念的影响。这种政治观念决定了波兰战俘的命运，并造成了他们之间巨大的差别。<br />
<br />
　　<br />
<br />
　　根据苏联当局的命令，被俘者中的将军、军官、军政要员、间谍人员、狱吏和警察等集中关押在指定的旧别尔斯克、科泽利斯克和奥斯塔什科夫三个专门战俘营。1939年11月初，进行并很快完成了相应的调动。<br />
<br />
　　旧别尔斯克和科泽利斯克营，关押的基本是波兰的原军官，他们的军衔从将军到上尉，军种也不同。另外还有一些军事技术人员如军医、教授，以及记者、画家和随军神父等。奥斯塔什科夫营主要关押着谍报人员、宪兵、狱吏、警察、地主、工厂主、定居者和难民等。1939年12月31日的统计表明，上述三个战俘营共关押15087人，到次年3月16日为14854人。<br />
<br />
　　随着战俘管理逐步走入正轨，一开始反映比较普遍的生活待遇问题慢慢减少，而战俘们要求给予公正政治待遇的问题日益突出。战俘们对被作为战俘关押提出质疑。曾在1939年9月指挥了利沃夫保卫战，后向苏军投降的西科尔斯基将军，10月20日致信乌克兰方面军司令员铁木辛哥，说“推迟释放我们使我们所有人及我们的家属处境极为艰难”，“关押在旧别尔斯克，并且个人自由在这里受到限制，使我们感到极为痛苦”。他希望苏方“采取一切可能的措施，使我们尽快获得自由”。1940年1月7日一批波兰上校上书战俘营，要求确定他们的地位和按照公认的国际准则对待战俘。他们直截了当地提出：“请向我们说明，苏联政府怎么看待我们，确切地说，就是：是否认为我们是战俘?”如果认为是，“我们请求根据各国政府公认的对待战俘的准则对待我们”，“使我们有可能自由求见受权驻苏联政府、负责保护波兰公民利益因而也保护战俘利益的大使馆”。一百多名军医和药剂师也致信伏罗希洛夫，声明是在“履行医生职责时被苏军俘获的”，要求按照日内瓦国际公约关于军事行动期间医生和药剂师权利的有关规定，将他们送往中立国。<br />
<br />
　　要求公正待遇的另一方式，是对关押制度的抵制与抗拒。战俘们有的组织“文化学习活动”小组，鼓动“只用波兰语交谈”，“不为战俘营上工”；有的以蹲禁闭室为荣，能够“不去干活，而且说服了其他人也不去干活”。还有战俘以绝食抗议伙食质量低劣。一些战俘策划在夏天逃出战俘营，投奔芬兰，因为“它正与苏联打仗，如果我们出现在芬兰领土上，何况我们还是军官，芬兰人将不会出卖我们，相反，会接收我们参军，委以军官职务”。某些战俘还酝酿大规模逃跑的计划，为此要消灭战俘营警卫队。战俘们的爱国情绪时有表露，他们认为，“波兰还会像过去的波兰一样存在”，并寄希望于英法来帮助“波兰复兴 ”；表示出对“正在为波兰的独立而拼杀”的士兵的崇敬。与这种爱国情绪很容易联系起的，是对苏联的不满。一些战俘结合当时苏联的对外政策指出，“苏联成了红色帝国主义国家”；还有战俘自己编辑了“充满了反苏精神”的报纸秘密传看。<br />
<br />
　　应当指出，上述情况并不是战俘营非常普遍的现象，因上述言行被战俘营管理当局处理的人员占的比例很小。但是，数量并不是一个问题意义的全部。正是在这些似乎不是普遍发生的问题上，集中代表和反映了苏联战俘管理体制与波兰战俘之间的矛盾。<br />
<br />
　　<br />
<br />
　　战俘营出现的上述情况陆续被上报到内务部。内务人民委员贝利亚在1939年的最后一天下达命令，要求在一个月内“查清他们在苏联国内和国外的全部联系，以及他们所知道的当时派往苏联的原波兰情报机关的间谍”。同时加快在战俘中寻找和发展为苏联服务的谍报人员工作。说穿了，就是波兰战俘的 “去”和“留”的问题。它们关系到关押在三座战俘营里的每个战俘的命运。战俘管理局很快整理出一份“1939年12月1日至31日期间各战俘营政治思想状况中的不良现象和特殊事件”的长篇通报，列举了各营反苏情绪和言论、自杀和企图自杀事件、逃跑和企图逃跑事件的情况。把此类材料集中在一起，给人留下深刻印象。此后各战俘营集中报告战俘负面情况的材料接踵而至。原来只在各战俘营例行报告中以“政治思想状况”为题出现的那些内容，现在直接换成了“不良情绪 ”、“反革命现象”、“特殊事件”等字眼，被汇集起来写成了通报和专门报告。它如同一架风向标，指明苏联领导人开始对哪类材料感兴趣。<br />
<br />
　　1940年2月20日，战俘管理局长索普鲁年科向贝利亚请示，要求给战俘营“减轻负担”。在贝利亚1938年接任内务人民委员后，给监狱和各种集中营“减轻负担”，是他推行的一条“新路线”。因此，索普鲁年科提出为战俘营“减轻负担”的建议，完全符合贝利亚的政策。在报告中，索普鲁年科建议：“将军官中的重病人、完全残废者、结核病患者、六十和六十岁以上的老人（约300人)全部释放回家。将家在乌克兰和白俄罗斯的预备役军官中没有劣迹材料的农艺师、医生、工程师与技术员、教师释放回家。据初步材料估计，这类人员可能释放400-500人。”对另一类人员即边防军军官、司法人员、地主、原波兰总参谋部二处军官、谍报军官等（约400人），他提议办理好案卷，“交内务人民委员部特别会议审查”。实际上，这是又一个分类处理方案，但却不是涉及全体战俘的方案。被建议释放的700-800人，加上需要继续审查的人员，不过1100-1200人，只是两座战俘营人员的一小部分，其他大部分战俘――主要是军官――怎样处理，索普鲁年科没有提及。仅两天后，贝利亚发出指示：“将关押在内务人民委员部旧别尔斯克、科泽利斯克和奥斯塔什科夫营中的原狱吏、谍报人员、奸细、定居者、司法人员、地主、商人及大私有者全部转押至监狱，移交给内务人民委员部各机构。他们的全部材料都移交给内务人民委员部州局的侦查部门进行侦查。这些案卷今后如何处理将另作指示。”3月2日，政治局通过“关于警卫乌克兰苏维埃社会主义共和国和白俄罗斯苏维埃社会主义共和国西部地区国界”的决定，建议内务人民委员部“从今年4月15日起，将被镇压和关押在战俘集中营中的原波兰军队军官、警察、狱吏、宪兵、情报人员、原地主、工厂主和原波兰国家机关高级官员等的家属，共22000-25000户，悉数迁往哈萨克苏维埃社会主义共和国各地区，为期10年”。<br />
<br />
　　三天后，一份决定波兰战俘最后命运的决定形成了。1940年3月5日，联共（布）中央政治局根据贝利亚给斯大林的一封信作出决议，斯大林在贝利亚的信上第一个签名并写下“同意”。政治局决议建议内务人民委员部：“（1）对战俘营中14700名原波兰军官、官员、地主、警察、谍报人员、宪兵、定居者和狱吏的案件；（2）以及对逮捕和关押在乌克兰西部和白俄罗斯西部各州监狱中的11000名各种反革命间谍组织和破坏团体成员、原地主、工厂主、原波兰军官、官员和越境分子的案件――以特别程序进行审理，对他们采用极刑――枪毙。”<br />
<br />
　　政治局的决议没有说明对上述人员采用极刑的理由，不过贝利亚在自己的信中回答了这个问题。他写道：“所有这些人充满了对苏维埃制度的仇视，是苏维埃政权的万恶敌人。”战俘营中的军官战俘和警察战俘，“他们每一个人都等待着获释，以便有机会积极地投入反对苏维埃政权的斗争”。贝利亚没有进一步列举“反革命活动和反苏宣传”的具体事例，这就使他的“理由”更接近定性性质，而不是论证性质。25700人的生命就这样被决定了。5月中旬，据统计，从三座专门战俘营被送去枪决的共14587人，加上其他地方送来的战俘，一共枪决了15131人。另有在西乌克兰和西白俄罗斯监狱关押的犯人 7305名，也一同被处决了。其中一部分被枪毙的波兰军官的尸体埋在了卡廷。<br />
<br />
　　<br />
<br />
　　1943年4月13日柏林电台向全世界报告的一则消息，说在卡廷森林发现了一批埋有上万波兰军官尸体的巨大坟墓，他们都是被人有步骤并且熟练地处决的。柏林电台说，这些坟墓中的一万多具尸体，是“犹太-布尔什维克兽行”的典型例证。那么，德国人是怎样发现这些被害者的呢？综合各方面材料，事情的经过大致是这样的：<br />
<br />
　　1941年7月德军占领了斯摩棱斯克，1942年秋，他们便从当地居民那里听到一些有关枪杀波兰战俘的传闻。但随之而来的冬季限制了他们查找和挖掘。1943年4月，天气转暖，大地解冻，德军开始了挖掘工作。随后有七个集体坟墓被发现，其中最大的一个估计有2000具波兰军官的尸体。发掘出的尸体制服，尤其是纽扣、军衔标志、装饰物、靴子式样等等，毫无疑问是波兰的。在尸体上发现的文件――日记、信件、报纸――的时间范围是 1939年秋天到1940年3、4月间。最晚的一份苏联报纸上的日期是1940年4月22日。挖掘出的尸体都是头部中弹而死，子弹都进入后颈，大多都是被一枪致命。根据弹着点特点可以判断，枪击是紧压着被害人后颈或在最近的范围内进行的。几个当地目击者也证实，在1940年3、4月间，几乎每天都有大量的波兰军官通过铁路运到卡廷附近的格涅兹多沃车站，然后被运送犯人的卡车运到卡廷森林，从此再也没有人见过他们。集体坟墓位于森林中部，坟墓上都是小松树。正是这些小松树也成了人们推定苏联人有罪的证明：如果波兰军官是德国人杀害的，他们不可能还会用小树来掩盖受害者的坟墓。其次，一个称职的植物学家只要检验一棵小树就会弄清这些树是1940年5月还是1941年7月以后的某个时候种的。<br />
<br />
　　苏联政府在沉默两天后发表公告，对德国的宣传给予反击。公告说，“德国法西斯恶棍在自己新的荒谬绝伦的臆想中并没有停止散布最荒诞不经和卑鄙下流的谎言，他们企图利用这些谎言掩盖由他们自己制造的滔天罪行，这一点现在已经很清楚了。”苏联的理由是，第一，在1941年夏，在红军撤出斯摩棱斯克地区后，从事建筑工作的波兰战俘和大批苏联公民一起落入德军手中；第二，德国人所说的发现大批遗骸的格涅兹多沃村是历史上有名的“格涅兹多沃墓葬”考古挖掘地，而德国的宣传对此避而不提。<br />
<br />
　　客观地说，苏联提出的上述理由并不具有多大说服力。苏联没有举出证据证明，他们所说的被德国抓住的波兰战俘和在卡廷被发现的死亡的波兰战俘是同一批人。其次，还有一个很好的佐证，波兰流亡政府曾数次询问过一批波兰军官的下落，当时苏联当局――包括斯大林的回答，都是已经释放了这些人，从未提起过被德国人俘虏了。至于考古挖掘就更经不起推敲，证明哪些是年代久远的遗骸，哪些是刚掩埋两年的尸骨，对于法医和生物学家并不困难。<br />
<br />
　　但苏联迅速利用了在反法西斯战争中获得的、并由刚刚结束的斯大林格勒会战胜利赢得了舆论支持，把任何对己不利的证据统统与希特勒的恶意污蔑联系在一起，而这种污蔑发生于德军惨败之后，更显合情合理。在一时无法确切得知真相的情况下，人们往往习惯于从已有和公认的事实出发进行判断，在这一点上，纳粹以往犯下的桩桩罪行，倒是比什么都具说服力。由此，“卡廷惨案”似乎已经有了公论，而真相也随之被深深掩藏起来。■</div>

							
							
							
							
