 <br />
在六三六五20周年到来之际，赵紫阳的录音回忆录《改革历程》出版了。原中共中央政治局常委秘书、中央政改办主任鲍彤先生说，“赵紫阳留下了一套录音带。这是他的遗言。赵紫阳的遗言属于全体中国人。以文字形式公之于世是我的主张，事情由我主持，我对此负政治上的责任。”<br />
<br />
鲍彤先生，在赵紫阳录音回忆录的《导语》中还进一步指出：“我不知道 在坦克和冲锋枪下伤亡同胞的数目。我国年年组织讨论日本侵略者杀死中国人的数目，从来没有谈论过中国人民解放军杀死本国人民的数目。……党中央开了武力镇 压公民的先例。二十年来，历届领导上台，都照例必须像宣誓一般，作出肯定镇压的赞美。上行下效，省、市、县、乡、村，创造了多少起官员镇压公民的小天安门 事件？有人说，一年三百六十五天几乎天天有。”<br />
