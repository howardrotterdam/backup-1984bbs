													<h2>赵紫阳口述痛史（二）：他们都整我　要我下台</h2>
						
						
																											<div id="postmessage_94013" class="t_msgfont">O果日螅2009年05月15日 <br />
<br />
<strong>－ 谣言中伤我以权谋私 －<br />
改革不能避免物价</strong><br />
<br />
第 13届全国代表大会在国内外备受好评，也令全国人民充满希望。 1987年的经济比往年为佳，国家经济持续快速增长，有迹象显示会继续平稳发展。假如我们依照过去几年成功经验，继续有关措施和政策， 1988年会持续改善。可惜事情并非这样。取而代之的是，好的变成坏的，到最後情况相当糟。<br />
<br />
物价飙升是 1988年的逼切问题，这是改革过程中不能避免的。 1988年首季物价续升，特别是食品价格，原因是前几年农产品产量一般。我们的方法也有问题，因为我们没按市场守则。谷物价格提升了，但肉类和蛋类没升，导致短缺。我们考虑几年内提升物价至正确水平，同时提高工资，但事後看来这是不切实际的想法。虽然双重定价制度制造了许多磨擦和贪污的机会，但亦为经济带来活力。我们明白价格改革至关重要。但由计划经济过渡到市场经济，必须循序渐进。 8月份，我们在北戴河讨论价格改革，立即有报章报道会有加价。我们忽略了储蓄的问题。这本是常识，但我们当时欠缺经验。民众开始恐慌，涌到银行提款和抢购货品，引致短缺，令经济情况看来似乎变坏。但事实并非如此，主要问题是心理因素。另一问题是对价格改革的不当报道。後来，当存储价值保证措施推出後，存款就回升了，情况迅速稳定下来。这证明经济并没重大问题。<br />
<br />
<strong>提五年整物价工资</strong><br />
<br />
挤提和抢购潮引致全国恐慌如浪潮涌至，各个主要城市都很紧张，党内外批评声音增加。这令我们高估了经济问题严重性，以为通胀率飙升。 1988年我们决定将重点从改革改为「调整及重组」，想稳定民众，但效果极之负面。李鹏和姚依林等人一直对改革有疑虑，「调整及重组」口号一出，他们立即收紧各方面的控制。几个月後，经济放缓，这一轮收缩终於持续两三年。到 1992年邓小平南下，批评「调整及重组」，经济立见起色，这亦证明之前经济情况并非真正严峻。这段时期有两个重要课题需重新考量：一是在双重价格制度下，改革必须循序渐进；事後回想，一切同时进行的基本方针都错了。二是在挤提和抢购潮後，高估了经济严峻情况。改变方向并不当，应是加深改革以稳定情况。问题是我们对价格改革的想法。我们试图强行突破，以为市场转型随後会完成。此外，价格改革的时机和公布都错了。设计丶讨论和落实价格改革的整个过程间，有些传媒报道邓小平的说话，另一些报道我的评语。这令焦点放在这问题。报道都说改革容易的部份完了，现在要搞定价格，这些都令人民恐慌。终於令反对改革的人有机会造成持续几年的经济滑落。 <br />
<br />
<strong>经改恐国企亏损</strong><br />
<br />
我在 1988年 5月 1日提报告，提出应在五年内调整物价和工资水平。国务院草拟了详细计划，交回政治局审议。邓小平一直支持价格改革。他曾公开说需要突破，我们要克服困难，又说问题不是太急进，而是踌躇。我知道他担心国有企业亏损和希望减少国家津贴。当然，若我们指出一些政策太难实行，他不会勉强。所以 1988年试图进行价格改革，责任不在他，完全在我。到最後，我提出押後改革，这也得到邓的同意。<br />
<br />
我们原本在 1988年计划了重大的改革和开放。 87年底提出了海南经济特区，拟出企业法；我提出让大型和中型国企采取自由管理，自负盈亏； 87年冬，也是沿海发展策略提出的时候，我提议将广东省作为改革政策试验场。<br />
<br />
假如这一些都顺利展开，就会有进一步改革和开放。可惜，因为价格改革失误，整个改革都受挫。现在回想，我仍非常惋惜。<br />
<br />
李鹏和姚依林利用「调整和重组」的机会，恢复老法子，把改革完全推翻，他们发出许多规例指令，收回下放的权力。权力集中在国务院几个人和中央委员会几个部门手上。结果经济迅速下滑。要不是有一些非国有的家庭生意和合资企业，全国经济可能会陷入极大困难。<br />
<br />
走向「调整和重组」政策後，人们相信我在经济事务方面势力减弱，我提的政策都没获得采用。一些高级同志认为我不再是总理，应专心党务，国务院对我的工作处处阻挠。连香港报章也指我已无实权，不再负责经济事务；有谣言说我将丢掉总书记的职位。「调整和重组」也令人以为经济改革遇上严重问题。<br />
<br />
一些高级同志要求政治局常委会──意思是说我──负起责任承认过错。这期间我从许多渠道听到有一群元老写信给邓小平，说我不够资格，应下台。这期间邓曾多次说「中央领导层架构不应改变」。<br />
<br />
<strong>说我子女卖车赚钱</strong><br />
<br />
一些党元老与李鹏等合作， 1989年春节前，李鹏和姚依林在政治局常委会会议上带头批评我，又问了好多奇怪问题，想整我。<br />
<br />
姚从来不说不含糊的话，这次他却很清楚直接，显得无畏。他和李鹏背後，一定有人支持。<br />
<br />
还有谣言中伤我和家人。有人说我的子女以权谋私：卖彩电丶汽车等赚钱。这些全是捏造的谎言。後来我下台，他们急急展开调查，但甚麽都查不到。<br />
<br />
以前我鲜有这类谣言，为何在 1988年下半年突然冒出？这不是意外，而是抹黑我的阴谋，要破坏我的改革派形象。<br />
<br />
<strong>邓对我的态度变了</strong><br />
<br />
1987年前我任总理，主要负责经济，不多参与政治。我比较慎言，胡耀邦却不同，他在意识形态上较自由。党内保守派主要针对他。他们觉得我相对中立。<br />
<br />
我当上代理总书记後，首先处理的是反自由化运动。他们列出名单，要批评这个那个。我不喜欢这种文革般的行为，加以阻止。结果六 四事件後我为此被批评。<br />
<br />
还有几个件令左派特别不满我，包括令邓力群丢了中央委员工作；左派结论是我「比胡耀邦更胡耀邦」。<br />
<br />
「调整和重组」提出後，他们认为我毁了经济政策和改革，於是散播不利我的谣言，展开「倒赵」运动。<br />
<br />
六 四前，邓一直支持我。但六 四事件後情况改变了。因为我和邓对於如何处理学生示威的方法意见不一。邓对我的态度变了，反对我的元老终於得到邓的同意，他们一起决定要让我下台。<br />
<br />
<strong>－ 邓小平要免去胡耀邦 －<br />
他最後不信胡耀邦了</strong><br />
<br />
胡耀邦在 1987年 1月被迫辞职，其中一个说法是邓小平受党内元老压力，要弃胡保赵，但我不认为这是真正原因，当然不少人曾在邓小平面前力数胡耀邦的不是，但邓小平放弃胡耀邦，并非被人误导或受压而作出妥协，而是他对胡的态度逐渐改变，到最後不再信任他。<br />
<br />
由 1980至 86年，邓胡的分歧逐渐扩大，其中我特别要提反精神污染运动，我认为胡处理此事的手法，深化了两人的冲突，邓支持这场运动，但耀邦一直有所保留，希望平息知识分子的不满和减少国际舆论的负面影响，他曾说过「清除精神污染」的口号并不恰当。<br />
<br />
1984年 6月 28日，邓单独向胡启立说，指耀邦未能坚守四个基本原则。 1985年 7月，邓小平指出真正问题是自由化趋势不断滋长，我认为鉴於邓多次强调这问题，书记处有需要召开会议以作回应，我向耀邦提出建议，但他只说要到新疆，未有慎重看待。<br />
<br />
1986年 9月，共产党 12届六中全会上，大家提出要在精神文明建设决议中，加入反自由化字眼，耀邦对此未有明确回应，但之後邓小平非常认真地说：「我讲反资产阶级自由化比任何人都多，我们不单现在要提，未来十年二十年也要继续提。」<br />
<br />
由於会议由耀邦主持，他模棱两可的立场令邓在最後关头介入，他明显是对胡耀邦不满。但其实在这事件前，邓已打定主意要除去耀邦，他已计划在第十三次全国党代表大会上重组领导层，平稳过渡。 <br />
<br />
<strong>胡见陆铿令邓小平愤怒</strong><br />
<br />
耀邦接受香港记者陆铿访问，也影响了邓胡二人的关系，邓认为耀邦在此事上非常不恰当，陆铿贬低了我们的国内和国外政策，他感到很愤怒。<br />
<br />
耀邦在 1987年请辞，但早在 1986年夏天，邓已打定主意除去他。<br />
<br />
1986年 12月，多个城市爆发学生示威，在上海，有示威者更冲击政府大楼，邓小平对此大为震惊，他召集大家到他家开会，指学生示威是反资产阶级自由化不力的後果。其实邓是要将这责任推给耀邦。<br />
<br />
1月 4日，我收到通知到邓的家中开会，他向我们展示一封信，是耀邦的辞职信，信中他提及自己的警惕性不足，做了很多愚蠢的事，未能确切执行四个基本原则和反自由化运动，令自由化泛滥。其後邓表示要以软方法处理耀邦的辞职，务求将影响减至最低。 1月 16日，我获委任为代总书记，但我表明我不适合担任此职，希望尽快找到一个合适的人选。 <br />
<br />
<strong>反自由化老人劲头很大</strong><br />
<br />
当时也有舆论指我曾写信给邓小平中伤耀邦，有人甚至指促请邓小平除去他，这绝对不是事实， 1984年我曾写过一封信，提到如何改善中央领导层体制，完全与耀邦无关。<br />
<br />
首先我要谈谈在耀邦 1987年辞职後发生的反自由化运动，我在 1987年的行动分为两阶段， 1至 4月，我要在全国推行反自由化运动；第二阶段是由 5月至十三大展开（ 10月），在这期间，我要重新强调改革，尝试避免左倾。<br />
<br />
而在 1987年中央领导班子改组丶耀邦辞职後，正面临着一个声势浩大的反自由化运动，在这情况下不反是不可能，当时有一种很大的力量，要乘反自由化来大肆批判三中全会的路线，要否定改革开放政策。<br />
<br />
『而我如何顶得住这势力，如何把反自由化控制起来。不使扩大化，不涉及经济领域，尽量缩小范围，减少一些思想混乱，这是一方面，再一方面就是对人的处理的问题，要不要处理人丶伤害人，如何少处理人，不过多伤害人，这也是我当时面对最头痛的问题。<br />
<br />
反自由化以来，一些老人们劲头很大，极左势力很大，想整很多人，邓小平一向主张对党内一些搞自由化的人严肃处理，王震等也是如此，邓力群丶胡乔木等人更是想乘机把这些人置於死地，在这情况下，如何在这次反自由化中少伤害人，保护一些人，这是一件比较麻烦的事。<br />
<br />
一开始，在制订中央四号文件时，为了少伤害人，对如何处理在反自由化中犯错的人作出严格的规定。文件提出：需要在报刊上点名批判和组织处理的，只是个别公开鼓吹资产阶级自由化丶屡教不改而影响很大的党员，并且应经中央批准。还指出，对有些持系统错误观点的人，可以在党的生活会上进行同志式的批评，允许保留意见，采取和缓的方式。<br />
<br />
我在宣传部长会议上和其他场合还讲了在思想文化领域要团结绝大多数人的问题，指出包括有这样或那样片面错误观点的人都要团结。我还指出，在从事思想理论文化领域工作的党员中，既鲜明坚持四项基本原则，又热心改革开放人固然不少，但也有些人拥护四项基本原则，而有些保守僵化；也有些人热心改革开放，而讲了些过的话，出格的话。既不要把前者看成是教条主义，也不要把後者看成是自由化分子，都是要教育团结的人。<br />
<br />
我当时有意识地强调反自由化时把有点自由化错误的人和有点僵化保守的人，都说成属於认识上的片面性，就是为了尽量避免或少伤害人。』<br />
<br />
在 1987年筹备十三大有两大重要事情，一个是草拟政治报告，另一个是填补领导层空缺，当我开始草拟政治报告。早在 5月 21日，我去信邓小平，提议在政治报告中使用「社会主义初级阶段」，有系统地涵盖理论丶原则和建立具有中国特色的社会主义工作，此外还提出「一个中心，两个基本点」。报告很快获邓的批准，并说大纲很棒。 <br />
<br />
<strong>走国情社会主义救中国</strong><br />
<br />
这些年来，改革证明是正确和必须，也证明了推行苏联的传统社会主义，窒碍中国社会经济发展和生产力，这是左倾错误。我们只要采取适当政策和适合中国国情的路线，才能救中国。<br />
<br />
『但是我们已经实行三十多年的社会主义，对一直遵循传统社会主义原则的中国人民，应该给个甚麽说法呢？<br />
<br />
一种说法是中国社会主义搞早了，该退回去，重搞新民主主义，一种说法是中国未经资本主义发展就搞社会主义，现在应当进行资本主义补课。<br />
<br />
这两种说法虽不能说没道理，但必然会引起很大争论，很可能在思想上造成新混乱。特别是这想法不可能得到通过，搞得不好会使改革开放事业夭折，因此不能采取。<br />
<br />
我在 1987年春季考虑十三大报告时，很长一个时期就考虑这个问题如何回答。在思考过程中我越来越觉得「社会主义初级阶段」这个提法最好。它既承认丶肯定了我们已搞了几十年的社会主义的历史，同时由於它是个初级阶段，完全可以不受所谓传统社会主义原则的约束，可以大胆调整超越历史的生产关系，从越位的地方退回去，实行适合我国社会经济水平和生产力发展需要的各种改革政策。』<br />
<br />
<br />
