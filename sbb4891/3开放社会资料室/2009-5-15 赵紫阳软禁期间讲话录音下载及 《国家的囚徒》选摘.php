													<h2>赵紫阳述连载</h2>
						
						
																											<div id="postmessage_94008" class="t_msgfont"><strong>赵紫阳口述痛史（一）：说我是黑手　邓小平镇压</strong><br />
<br />
O果日螅2009年05月15日 <br />
<br />
<strong>－ 悲剧终发生　全世界震惊 －<br />
邓：快刀斩乱麻化解形势</strong><br />
<br />
学生抗议的初期，事件都是与悼念（前总书记）胡耀邦有关。耀邦在 1989年 4月 15日逝世。宣布广播後，部份大学生自发悼念，不久上街活动，参加者数目越来越多。悼念会结束後，我提出三点意见：第一，追悼活动已经结束，社会生活应纳入正常的轨道，对学生游行要坚决劝阻，要让他们复课；第二，对学生采取疏导方针，应开展多层次丶多管道和各种形式的对话，互相沟通，增进理解，寻求不同意见。第三，无论如何要避免流血事件，但对参与打丶砸丶抢丶烧丶冲五种行为的人应依法惩处。<br />
<br />
当时（国务院总理）李鹏和所有政治局常委，都接纳我的建议。我之後听到李鹏向邓小平报告这三点，他也表示同意。那时政治局常委没有不同意见，至少没有公开提，只是李鹏 4月 19日突然打电话来说：「学生想冲入新华门了，为甚麽没有对应行动？」<br />
<br />
我在 4月 19日见邓小平，讨论访问朝鲜的事，跟他谈到学生示威，我对他提出处理局势的意见。当时邓小平表示支持我，但之後情况奇怪地转变。<br />
<br />
在我离开北京後（ 4月 23日）的第二晚，李鹏召开政治局常委会议，（北京市委书记）李锡铭和（北京市市长）陈希同大力报告学生示威情况严重，他们没有理会学生示威已降温的事实，他们谴责少数学生的极端意见，特别是针对邓小平的话。他们将示威说成是反党和针对邓小平个人。<br />
<br />
<br />
<strong>4． 26社论跟人民意愿矛盾</strong><br />
<br />
4月 25日，李鹏和（国家主席）杨尚昆向邓小平报告政治局常委会议，邓小平一直倾向强硬处理学生示威，因为他认为示威妨害稳定。听了他们报告後，邓小平立即同意将学生示威定性为「反党反社会主义动乱」，提出尽快化解形势，要「快刀斩乱麻」。<br />
<br />
邓小平的讨论原本是内部事务，李鹏却决定将他的讲话内容发放给各级党干部，并 4月 26日《人民日报》社论（又称 4． 26社论）引述他们的说法。 4． 26社论令对立升级，学生上街人数大增，也令各个政府部门丶组织和其他政党不满。<br />
<br />
我 4月 30日从朝鲜回来後，从各方面搜集资料，评估形势後，我更强烈相信学生示威得到社会各界广泛同情， 4.26社论和中央委员会处理示威的手法，跟人民的意愿矛盾。我也觉得如果学生示威能以民主和法治的原则化解，透过对话和淡化矛盾化解，这可能会推动中国改革，包括政治改革。不过，问题的关键在於邓小平本人，他拒绝放宽他的立场的话，我就无法改变强硬派的态度，他们不改变立场，政治局常委会就难执行淡化矛盾和开放对话的原则。 <br />
<br />
<strong>邓一锤定音派军队进京</strong><br />
<br />
我在 5月 4日对亚洲开发银行代表团的讲话，表达了需要以冷静丶合理丶克制丶有秩序丶根据民主和法治的原则，去化解问题。不过李鹏集团的人主动尝试去阻止丶拖延丶甚至搞破坏， 5.4讲话提出对话和淡化矛盾的原则，无法落实。学生对跟政府对话不再抱任何幻想，发起大型游行和绝食。<br />
<br />
在 5月 17日的政治局常委扩大会议，邓小平一锤定音。他说：「形势发展证实 4.26社论的判断是正确的。学生游行无法消退是党内部的问题，那就是赵紫阳的 5.4讲话。现在要避免局势失控，已无路可退，决定就是军队开进北京戒严。」<br />
那时我非常不开心，我对自己说无论如何，我拒绝成为动员军队镇压学生的总书记。回家後我情绪激动，就叫鲍彤草拟辞职信，准备交给政治局常委会，听了杨尚昆劝喻後收回。<br />
<br />
5月 19日凌晨我去天安门广场探望学生，李鹏反对，想阻止。我认为这麽多学生绝食长达 7日，中央委员会没有一个领导人去探望，说不过去。我只是想劝学生停止绝食，对他们说他们仍年轻，要珍惜生命。不过学生不了解我的意思，我之後受到严厉批评，也开始发现自己被排除出决策外。 <br />
<br />
<font color="red"><strong>我在院子乘凉听到密集枪声</strong></font><br />
<br />
『 6月 3日夜，我正同家人在院子里乘凉，听到街上有密集的枪声，一场举世震惊的悲剧终於未能避免地发生了。<br />
六 四悲剧发生三年後，我记下了这些材料，这场悲剧已经过去好多年了。这场风波的积极分子，除少数人逃出国外，大部份人被抓丶被判丶被反覆审问。情况现在是非常清楚了，应该说以下三个问题可以回答了：<br />
<br />
<font color="blue">第一，当时说学潮是一场有领导丶有计划丶有预谋的「反党反社会主义」的政治斗争。现在可以问一下，究竟是甚麽人在领导？如何计划，如何预谋的？有那些材料能够说明这一点？还说党内有黑手，黑手是谁呀？<br />
<br />
第二，说这场动乱的目的是要颠覆共和国，推翻共产党，这方面又有甚麽材料？我当时就说过，多数人是要我们改正错误，而不是从根本上推翻我们的制度。这麽多年过去了，审讯中得到甚麽材料？道是我说得对还是他们说得对？许多外出的民运分子都使他们和党处在对立的方面。在学潮期间，学生提出过很多口号丶要求，但就是没有提物价问题，而当时物价问题是社会很大的热点，是很容易引起共鸣的。学生们要和共产党作对，这麽敏感的问题他们为甚麽不利用呢？提这样的问题不是更能动员群众吗？学生不提物价问题，可见学生们知道物价问题涉及改革，如果直接提出物价问题动员群众，实际上是要反对丶否定改革。可见不是这种情况。<br />
<br />
第三，将「六 四」定性为反革命暴乱，能不能站得住脚？学生一直是守秩序的，不少材料说明，在解放军遭到围攻时，许多地方反而是学生来保护解放军。大量市民阻拦解放军进城，究竟是为甚麽？是要推翻共和国吗？当然，那麽多人的行动，总有极少数人混在人群里面攻打解放军，但那是一种混乱情况。北京市不少流氓丶流窜犯乘机闹事，那是完全有可能的。难道能把这些行为说成是广大市民丶学生的行为吗？这个问题到现在应该很清楚了。』</font><br />
<br />
政治局 6月 19日至 21日召开扩大会议。李鹏的报告为会议定调，指我犯了「分裂党」和「支持动乱」的错误。他建议除去我的党总书记丶政治局和政治局常委职务。他们要求我写自我批评书，我不肯，在会议最後要求发言，说出我准备好的辩护讲话。我早有心理准备，我宁愿下台，都不跟从他们的看法。<br />
<br />
<strong>－ 国家软禁我是蔑视法律 －<br />
赵紫阳的孤独抗争</strong><br />
<br />
在当局快要宣布结束对我的调查之际，他们立即订下六大规条限制我的行动自由，但当局在执行六大规条时，从没给我任何文件或口头通知我，可能是感到内疚，或者担心这些证据会被外国媒体取得，成为国际传媒焦点，再出口转内销。<br />
他们指示安全部或我家的下属执行规条，由於他们不能对我说，每当我问到他们时，他们总是编造藉口应酬我，往往令我跟他们起冲突。最後，我发现这些规条，我不再怪他们，他们只是奉了上头命令行事。<br />
<br />
我觉得他们应该早点对我说那些规条：甚麽是准的，甚麽是不准的，我自会明白。譬如，调查结束後，我曾要求到广东避寒，因为我气管有事，他们说港督彭定康正在扩大民主选举，广东形势复杂，我不方便去。<br />
<br />
我认为这很荒谬，彭定康所搞的属外交事务，关广东甚麽事？他们坚持说，广东是去不得，广西丶云南丶贵州却可以去。我决定去广西，但只局限南宁市，抵夺嵛腋发现我去那儿都遭到阻挠。<br />
<br />
返回北京，我说我想去一个老党员常到的俱乐部打桌球，他们最初拒绝，但最终放行，去到时发现俱乐部空无一人，原来他们把俱乐部清了场，怕我碰见老朋友。<br />
<br />
当局关注我外出的「影响」只是推搪的藉口，目的要不让我再公开露面，让人民渐渐淡忘我，所谓「担心影响」只是担心我的影响力仍在，就算是我的名字也足以引发社会不安。<br />
<br />
我曾去信江泽民要求解释六大规条，结果当然是没有回覆。当局在处理我的问题都经常显得很紧张不安。当局说我可以在家中见客，条件是客人不能是记者和外国人，但实际上是从来都不准访客探我。<br />
<br />
1995年秋天，陈云逝世，我想亲身去慰问他的遗属，却被禁止，说「不方便」，我以为陈云的家属拒绝，後来发现原来他们是愿意见我的。<br />
<br />
当邓小平在 1997年逝世时，我身在杭州，听到消息我很悲伤，我致电当局要求即时返回北京出席悼念仪式，中委会的回应却是没有送别邓小平的仪式，叫我不用回北京。<br />
<br />
1997年 5月，当彭真逝世，我向他的家人说我想上门探望，但保安局却说彭的家人未布置好灵堂，叫我在「顾全大局」情况下，最好不要去了。我对此非常愤怒，为何不准我悼念老同志之死？他们有甚麽好怕呢？<br />
<br />
1997年 9月 12日，我去信第 15大，要求重新评价六 四事件，指事件虽然过了多年，但人民是不会忘记，最好尽快重新评价。我没有公开这封信，但海外传媒却知道了。当局十分不满，指我不服从党纪，之後一名老同志逝世，他们乾脆不准我出席丧礼作为惩罚。<br />
<br />
我多番去信当局，要恢复我的人身自由，却杳无音讯，我决定去信常委会所有成员，提出软禁我是违法。当局後来解释，从来没有「软禁」我，又说我是自作自受，暗示我把致党大会的信外泄。<br />
<br />
之後，江泽民访美前，中南海派来的医生忽然对我说，我整天留在家对身体无益，说要上报。为何连医生也关注我的自由？我想起了，江泽民考虑到访美时必被问到我的情况，故先造成我可以自由活动的事实，好让他有所交代。想到这点，我没有理会医生的提议， 1997年的冬天我没有外出，整个冬天都留在北京。 <br />
<br />
<font color="red"><strong>拒当镇压学生的总书记</strong></font><br />
<br />
1989年中共第 13届四中全会召开，免去我所有职务，决定对我进行调查，对我的处置是「史无前例」的，而党在未有调查结果前重罚我，也是对我的「特别待遇」。<br />
<br />
四中全会决定成立特别调查小组，正式调查我。调查历时三年零四个月，调查组集中调查我是否直接或间接操控六三六五民运，也想知道为甚麽我的政策和立场，跟中共元老邓小平的对着干。<br />
<br />
小组认定我有「不可告人的动机」与「个人野心」，引用所谓证据加上个人臆测，断定我搞新权威主义。他们说， 1988年起国内外已出现一场「倒邓拥赵」运动，又指我是令中国恢复资本主义的理想人选。<br />
<br />
我对指控一一以口头和书面方式回覆王任重（时任赵紫阳专案组组长）。我说，其实当时有关我地位不稳的谣言，比「倒邓」传闻更厉害，但我说六 四事件发生前，邓小平多次表示支持我，即 1989年初李鹏和姚依林（时任中共政治局常委丶国务院副总理）都批评我经济搞不好，邓一样站在我的一边，反叫他们要支持我。<br />
<br />
我向王任重解释，拒绝接受邓小平对学运的回应，是因为同邓在学运性质和镇压後果上有不同意见。<font color="red"><strong>我说：「我认为我要对历史负责。我拒绝当一个镇压学生的总书记。」 </strong></font><br />
<br />
<strong>外出打高球江泽民忧心</strong><br />
<br />
这段被调查时间，我的行动自由一直被剥夺，我指出软禁我是蔑视法律， 1990年我三次去信江泽民（时任中共总书记）丶李鹏和政治局常委，要求终止调查和软禁，恢复我的自由，但信件石沉大海，他们没有回应我。<br />
<br />
1990年 10月亚运会举行前，我决定出去打高尔夫球，安全部门听到消息後，要求我的秘书阻止我出去，我说他们没有法律理据阻我出去。他们叫司机不要送我，我说那我就乘公车去吧。最後安全部门让我外出，却由公安车护送。<br />
<br />
外国媒体知道了我打高尔夫的消息後大肆报道，江泽民和李鹏忧心忡忡，责怪安全部门批准我外出。之後，当局口头通知我，在调查期间我不准外出。政治局委员乔石後来对我说，要我顾全大局，说我的情况很敏感，叫我不要做出任何损害稳定的事。<br />
<br />
1990年 6月 21日，王任重把调查报告草稿交给我，列出 30项证明我「支持动乱」和「分裂党」的「事实与证据」。<br />
<br />
