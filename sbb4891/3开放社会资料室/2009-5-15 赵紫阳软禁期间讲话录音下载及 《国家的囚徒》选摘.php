													<h2>赵紫阳口述痛史（三）：无名恐惧　改革太难了</h2>
						
						
																											<div id="postmessage_94014" class="t_msgfont">O果日螅2009年05月15日 <br />
 <br />
<strong>－ 一旦涉及外国　就怕丧权辱国 －<br />
领导层经改立场南辕北辙</strong><br />
<br />
在（ 1978年） 11届三中全会後，中央领导层对改革存在两派观点：一派是以邓小平为首，主张加快对外开放，朝市场经济方向进发来扩充经济；另一派是以陈云为首，坚持计划经济为本，对经改有所保留。<br />
<br />
人民一直全力支持邓小平的改革。我对此充满热忱，并努力落实。但是，对於邓强调速度有所保留。当然，若所有都进展顺利，经济运行无阻，越快越好，毋庸置疑。但是，从过去集中追求出产价值的错误中，我学懂了，过份强调速度，最终会变成盲目追求高目标及速度，代价是失去效率。我的生产目标较温和，强调经济效益。邓理解我的观点，在这方面没有冲突。在改革方面，（胡）耀邦跟我基本立场一致。我俩都充满热忱，但在个别步骤丶态度和方法，特别是速度上，耀邦比邓更激进。<br />
<br />
陈云同志对开放政策深感不安，跟邓小平的分歧颇为明显。原本计划沿海地区有多个经济特区，包括上海及浙江省，但陈云指那些地区不会成为经济特区，因为这些区域出了名多机会主义者，透过绝顶技巧，即使只有很小的机会，都可以从笼子扑出来。<br />
<br />
<strong>霍英东教租地发展城市</strong><br />
<br />
『现在回想起来，中国实行改革开放实在不容易，阻力很大，顾虑很多，很多无名恐惧，给要做这些事的人戴了很多帽子。改革开放，尤其是开放很不容易。一涉及到与外国人的关系，总怕丧权辱国，怕自己吃亏，说「肥水不流别人田」。所以我常给他们讲这个道理：外国人到中国投资，他们本来就很多顾虑，我们的政策这样不稳定，应该说有很多风险，要怕的应该是拿钱进来的外商，我们中国政府有甚麽可怕的呢？」』<br />
<br />
大约是 1985或 1986年，我跟（香港爱国商人）霍英东谈话，提及我们没有资金作都市发展。他问我：「你若有地，怎会没有钱？」我觉得此话很奇怪。有地是一件事，缺乏资金是另一回事，两者有甚麽关系？他说：「市政府若有地，他们应获准将部份租出去，带来一些收入，再让其他人发展那块地。」<br />
<br />
当然，我知道香港的建筑物及街道是如何快速建成，一处地方如何快速变化，但对我们来说，那非常困难。我认为他说的有道理，所以我建议他去上海，跟市长及党委书记谈谈。我不知道他有没有去。他的观点启发了我的思考。 <br />
<br />
<strong>经改追求人民得到实惠</strong><br />
<br />
『也许有人会问，你过去在地方工作，怎麽对经济改革发生兴趣？我认为中国经济必须改革，虽然那时我也看过一些东欧经济改革的书，但出发点不是为了改革而改革，主要的是我认为中国的经济弊端太多，人民付出的代价太大，效益太差。但弊端的根本在那里，开始也不是很清楚。总的想法就是要提高效益。来北京後，我对经济工作的指导思想，明确地不是为了追求产值多少，也不是要把经济发展搞得多快，就是要在中国找到一个如何解决人民付出了劳动，而能得到相应的实惠的办法，这就是我的出发点。资本主义发达国家经济增长 2-3%就不得了，而我们经常增长 100%，但人民生活没有得到改善。至於怎样找到一条路子，我当时观念里没有甚麽模式，没有系统的主张。我就是希望经济效益好，有这一条很重要。出发点就是经济效益好，人民得到实惠。为了这个目的，摸索来，摸索去，最後就找到了适合我们的办法，逐渐走出了一条路。』<br />
<br />
中国如何会采取渐变主义？我在中央委员会主管经济的 10年，我们奉行逐渐过渡。这个有两方面。首先是出现一个新的市场经济，在计划经济系统以外逐渐成熟。另方面是减少计划经济部份。改变不是即时的，而是由小量微细的改变开始，逐渐涉及较大的改变。<br />
<br />
<strong>融入国际市场才能现代化</strong><br />
<br />
很多年来，我们的经济发展成效欠佳，付出要求很大，但回报很小。除了经济制度，还有其他问题，例如闭关政策，令自给自足变成绝对美德。这成为了一种意识形态，被政治化。但所有例子都证明，只有在开放政策下，我们才可以利用所拥有的，透过交易获取我们需要的。<br />
<br />
各个地方和社会都有它的强项，即使贫穷地区都有它们的优势，例如廉价劳工。这是国际竞争的大好处。所有东西都靠自己，结果是做不到自己最擅长的，我们亦因而承受过巨大损失。我越来越明白，若一个国家关闭起来，不融入国际市场，或不好好利用国际贸易的好处，最终只会落後於人，没有可能达致现代化。<br />
<br />
加快沿海地区发展，不只有利沿海地区，更会推动全国经济，包括内陆省分。没有沿海地区的发展，所有流动工人要去那里找工作？沿海地区发展了，劳工密集生产的规律就会在全国实施，转移到劳工更便宜的地区。劳工成本开始在沿岸地区上升，生产工作就要被迫作出调整。<br />
<br />
所以，我们不可以全国单一速度发展，而是需要由一个地区带动另一个地区发展。全国单一行动代表没有一个地区能较快发展。沿海地区是中国一部份，它们的长处若得以利用，全国都有益处，包括中部和西部地区。以整体发展观点看，有需要优先发展沿岸地区。<br />
<br />
<font color="blue"><strong>司法制度独立才能肃贪</strong></font><br />
<br />
我越来越明白到，「保持清廉」很重要。若忽视了，就会给那些反对改革的人口实，同时会令人民不满。後果可以很严重。同时，我们亦可利用反贪，作为深化改革的机会。由於这些问题在新环境下出现，不能以旧方法解决。首先，必须理解这些问题的成因。只有这样，我们才可对症下药。贪腐一般在经济文化发展到某一阶段时出现，但之後，当公务员及执法人员的素质提升，他们的薪酬及福利增加，情况就会改变。今日的香港跟以往很不同。同样的情况，亦在其他发展中国家发生。另一样很重要的，事实上亦是最重要的，是要有独立的司法制度及法治。假如没有独立的执法机关，而执政党又有能力干预司法制度的话，贪腐问题将永远无法有效解决。<br />
<br />
<font color="red"><strong>－ 议会民主制实现现代的法治 －<br />
邓鄙视权力受监察的制度</strong></font><br />
<br />
由 1980至 1989年 6月 4日之前，邓小平重复提及反对自由化。另一方面，他又说了多次，政治改革是必须的。那样改革应该怎样根据邓小平的思想实践呢？<br />
<br />
大部份人对於邓小平的政治改革思想的印象，均来自 1980年 8月他在中共中央政治局扩大会议上的讲话《党和国家领导制度的改革》。他批评官僚主义丶权力过度集中，以及爱国主义，这些都是当时中共政治制度的一部份。他指出，这些问题的根源存在於原有的制度；好的制度防止了不良分子任意妄为，不完善的制度则限制了人才实现良好愿望，甚至把人才推向相反方向。<br />
<br />
有些人觉得邓小平在政治改革上只是信口雌黄，为了给人民一个好印象。有些则相信邓小平的政治改革永远不能实施，因为他们都会被（政治）情况或反对势力阻挠。我相信这两种想法都缺乏证据。<br />
<br />
邓小平很推崇一些专制社会主义国家的政治改革，很鄙视权力受监察及平衡的政治制度。他有一次跟南斯拉夫的访客说：「社会主义国家的其中一个最大优点是，当一些东西已经决定，就可以不受限制的即时落实，不似得国会民主程序那样复杂，进退两难，议而不决，决而不行。」 <br />
<br />
<strong>胡耀邦没下台政改可向前</strong><br />
<br />
胡耀邦被邓小平多次指控为纵容资产阶级自由化，因而被迫下台。人们对胡耀邦的印象，一般都是改革派及民主派。胡耀邦的政治改革思想是怎样的呢？他提出过甚麽？<br />
<br />
毋庸置疑，耀邦寻求过民主。即使他没有触及一个特定的结构或社会主义民主制度的模型，我相信如果他仍能继续出任党和国家的领导──以国家及海外的世界趋势而言──他定能在政治制度现代化及民主化的路线上，把中国政治改革推动向前。<br />
<br />
第 11届中央委员会第三次全体大会（ 1978年）之後，我的精神集中在经济改革，忽视了政治改革问题。即使我在四川的时候，实现了扩大企业自主，我仍然从没想过怎样实施政治改革。一位陕西工人曾写信给我，说他阅读过我的多次讲话，相信我在经济事务上是一个改革家，但在政治议题上是个保守派。<br />
<br />
<font color="blue"><strong>社会主义国家需法治非人治</strong></font><br />
<br />
直至 1985丶 86年，我的思想才改变。我受到更广的国际环境，及东欧出现的情况影响。而改变的主因是，我发现为了经济改革的前景，有需要政治改革。当经济改革深化，党内保守势力的阻力更加严重。没有政治改革，经济改革很难持续。（ 1989年）我跟（前苏联领袖）戈尔巴乔夫说过两点：第一是共产党的执政地位不会变，但它的管治方式必须改变；第二是社会主义国家应该行法治，非人治。<br />
<br />
首先，我们需要提高党与国家决策的透明度。戈尔巴乔夫说这是「开放」，我们叫做「透明」。党与国家的重大活动与决定需要公开，这样可以改变长期存在的黑箱作业丶公众只知道最後决定的情况。政府宣布了决定就付诸实行，但人民不知道决定的过程。这是很重要的，人民有权知道。其次，我们需要建立一个多元的对话渠道：跟不同的社会派系丶势力与利益团体。作出重大事情的决定之前，应该与不同的社会组织持续的谘询与对话，不是只跟其他政党的代表人物对话一次就作决定。<br />
<br />
<strong>中共元老怕政改失去政权</strong><br />
<br />
第 13届中共党大会（ 1987年），我们不仅讨论了经济改革，也讨论了政治改革。我们提出如何改善社会主义民主制度。那时候，政治环境较宽松，形而上学与理论研究丶文化与艺术都很活泼。与此同时，十年改革期间，我们受到西方的价值丶概念与政治制度的影响。再者，苏联针对政治异见人士的政策已经开放了。所有这些事情都鼓励着中国知识分子丶青少年和年轻工人要求更多民主。<br />
<br />
然而，第 13届党大会之後，政治改革变得困难了。首先，党内的元老，包括邓小平同志，对经济改革有不同意见，但对政治改革只有一个意见：他们反对改变现有制度的基础。他们害怕任何真正的政治改革，都会导致共产党的权力被挑战，因而削弱了党，甚至导致共产党失去执政地位。几乎没有党元老支持这种改革（执政者须接受监察），现实是，政治改革陷於停顿。这样造成了一个问题，一方面，人民对民主及政治加速改革的要求越趋强烈；另一方面，第 13届党大会以来，从来没有政治改革的行动。党的意向与人民的要求丶尤其是知识分子的要求，出现了鸿沟。 <br />
<br />
<strong>李先念反赵行动非常积极</strong><br />
<br />
李先念在「反赵」行动中非常积极，他既是前线人员，也是幕後主脑。 1988年 10月，第 13届中央委员会第三次全体大会上，曾有计划公布架构重组。但王震（党元老）突然攻击《河殇》，要求中央委员会正式发出批判。我没有理会。<br />
那次事件之後，叶选甯（叶剑英之子）告诉我，王震当着他面前极力告发鲍彤，说他是支持制作《河殇》的无赖──这是王震从李先念口中听到的指控。<br />
<br />
事实上，这是完全虚构的。鲍彤从来没有干过任何与《河殇》有关的事情，甚至说过关於《河殇》的话。<br />
<br />
王震没说的比说了的更多。当李先念提及鲍彤，实际上就是在说我，暗示我支持了《河殇》。王震本人可能不高兴《河殇》，李先念则藉此机会把我连系上来，激发王震对我的怒意。为了激发元老对我的不满，李先念I造事实也在所不惜。<br />
六 四之後，当他们在报章刊登了对我的批判时，《河殇》是一个主要话题。<br />
<br />
很多指控都是完全虚构的，例如说我曾支持《河殇》的制作，说我下令把影带发布至全国，并遏止所有批评制作《河殇》的声音。所有不是正确的。<br />
<br />
<strong>世界潮流浩浩荡荡逆之亡</strong><br />
<br />
当然将来有一天也许会出现比议会民主制更好丶更高级的政治制度，但那是将来的事情，现在还没有。<br />
<br />
基於这一点就可以说，一个国家要实现现代化，不仅要实行市场经济，发展现代的文明，还必须实行议会民主制这种政治制度。不然的话，这个国家就不可能使它的市场经济成为健康的丶现代化的市场经济；也不可能实现现代的法治社会。就会像许多发展中国家，包括中国出现权力市场化，社会腐败成风，社会两极分化严重的情况。<br />
<br />
如果我们按良心做事，对党丶社会和人民都有好处，任何其他方式都会有害。<br />
<br />
潮流不可挡，适者生存。<br />
<br />
正如孙中山说：「世界潮流浩浩荡荡，顺之则昌，逆之则亡。」<br />
<br />
