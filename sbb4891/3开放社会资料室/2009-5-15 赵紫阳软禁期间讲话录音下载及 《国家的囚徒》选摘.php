第三，将“六三六五”定性为反革命暴乱，能不能站得住脚？学生一直是守秩序的，不少材料说明，在解放军遭到 围攻时，许多地方反而是学生来保护解放军。大量市民阻拦解放军进城，究竟是为了什么？是要推翻共和国吗？当然，那麽多人的行动，总有极少数人混在人群里面 攻打解放军，但那是一种混乱情况。北京市不少流氓、流窜犯乘机闹事，那是完全可能的。难道能把这些行为说成是广大市民、学生的行为吗？这个问题到现在应当 很清楚了。<br />
<br />
 二<br />
<br />
以上几点，就是说明1987年中央领 导班子改组、耀邦辞职以后，面临着一个声势浩大的反自由化运动。在这种情况下，不反是不可能的。当时有一种很大的力量，要乘反自由化来大肆批判三中全会的 路线，要否定改革开放政策。而我如何顶住这股势力，如何把反自由化控制起来。不使扩大化，不涉及经济领域；尽量缩小范围，尽量减少一些思想混乱，这是一个 方面。再一个方面就是对人的处理的问题。要不要处理人、伤害人。如何少处理人，不过多伤害人，这也是我当时面对最头痛的问题。<br />
<br />
反自由化以来，一些老人们劲头很大，极左势力也很大，想要整很多人。邓小平一向主张对党内一些搞自由化 的人作出严肃处理。王震等其他几位老人也是如此。邓力群、胡乔木等人更是想乘机把这些人置于死地而后快。在这种情况下，如何在这次反自由化中尽量少伤害一 些人，保护一些人，即使没法避免也力求伤害得轻一些，这是一件比较麻烦的事情。一开始，在制定中央四号文件时，为了少伤害一些人，对如何处理在反自由化中 犯错误的人作出了严格的规定。文件提出：需要在报刊上点名批判和组织处理的，只是个别公开鼓吹资b阶级自由化、屡教不改而影响很大的党员，并且应经中央批 准。还指出，对有些持系统错误观点的人，可以在党的生活会上进行同志式的批评，允许保留意见，采取和缓的方式。我在宣传部长会议上和其他场合还讲了在思想 文化领域要团结绝大多数人的问题，指出包括有这样或那样片面错误观点的人都要团结。我还指出，在从事思想理论文化领域工作的党员中，既鲜明坚持四项基本原 则，又热心改革开放的人固然不少，但也有些人拥护四项基本原则，而有些保守僵化；也有些人热心改革开放，而讲了些过头的话，出格的话。既不要把前者看成是 教条主义，也不要把后者看成是自由化分子，都是要教育团结的人。我当时有意识地强调反自由化时把有点自由化错误的人和有点僵化保守的人，都说成属于认识上 的片面性，就是为了尽量避免或少伤害人。<br />
<br />
三<br />
<br />
但是，我们已经实行了三十多年的社会主义，对一直遵循传统社会主义原则的中国人民，究竟应该给个什么说 法呢？一种说法是，中国社会主义搞早了，该退回去，重搞新民主主义；一种说法是，中国未经资本主义发展就搞社会主义，现在应当进行资本主义补课。这两种说 法虽然不能说没有道理，但是必然会在理论上引起很大争论，很可能在思想上造成新的混乱。特别是这样的提法不可能得到通过，搞得不好会使改革开放事业遭到夭 折，因此不能采取。我在1987年春季考虑十三大报告时，很长一个时期就考虑这个问题如 何回答。在思考过程中我越来越觉得“社会主义初级阶段”这个提法最好。它既承认、肯定了我们已搞了几十年的社会主义的历史，同时由于它是个初级阶段，完全 可以不受所谓传统社会主义原则的约束；可以大胆地调整超越历史的生b关系，从越位的地方退回去，实行适合我国社会经济水平和生b力发展需要的各种改革政 策。<br />
<br />
四<br />
<br />
也许有人会问，你过去在地方工作，怎麽对经济改革发生兴趣？我认为中国经济必须改革，虽然那时我也看过 一些东欧经济改革的书，但出发点不是为了改革而改革，主要的是我认为中国的经济弊端太多，人民付出的代价太大，效益太差。但弊端的根本在哪里，开始也不是 很清楚。总的想法就是要提高效益。来北京后，我对经济工作的指导思想，明确地不是为了追求b值多少，也不是要把经济发展搞得多快，就是要在中国找到一个如 何解决人们付出了劳动，而能得到相应的实惠的办法，这就是我的出发点。资本主义发达国家经济增长2-3%就不得了了，而我们经常增长10%，但人民生活没有得到改善。至于怎样找到一条路子，我当时观念里没有什么模式，没有系统的主张。我就是希望经济效益好，有这一条很重要。出发点就是经济效益好，人民得到实惠。为了这个目的，摸索来，摸索去，最后就找到了适合我们的办法，逐渐走出了一条路。<br />
<br />
<br />
五<br />
<br />
总之，当时有两部分，一个是计划体制外的市场经济，一个是计划体制内的计划经济。我们一方面扩大计划外 的市场经济，另一方面逐步缩小计划经济的比重。在计划经济和市场经济并存的情况下，势必是此消彼长。计划经济缩小减弱，市场经济就得到扩大和加强。当时市 场经济部分主要是农业、农副b品、轻纺工业、消费品工业，而属于生b资料生b的，基本上掌握在国营企业手里。一个消费品，一个生b资料，如果控制生b资料 生b的企业不削弱、不缩小，不分出一部分投入市场，新生长起来的那一部分市场经济就无法继续；如果生b资料生b的那一部分，一点也不允许自销，一点也不允 许进入市场――如果把小煤窑、小水泥也都统管起来的话――那新生长的市场经济将会因缺乏原材料而遭遇到极大的困难。所以十几年来对计划内经济体制的改革， 对国有企业机制的改革，尽管都没有触动根本，但从中国由计划经济向市场经济过渡这个意义上看，它起了不可忽视的良好作用。<br />
<br />
 六<br />
<br />
当然将来哪一天也许会出现比议会民主制更好、更高级的政治制度，但那是将来的事情，现在还没有。基于这 一点就可以说，一个国家要实现现代化，不仅要实行市场经济，发展现代的文明，还必须实行议会民主制这种政治制度。不然的话，这个国家就不可能使它的市场经 济成为健康的、现代化的市场经济；也不可能实现现代的法治社会。就会象许多发展中国家，包括中国出现权力市场化，社会腐败成风，社会两极分化严重的情况。<br />
<br />
七<br />
<br />
现在回想起来，中国实行改革开放实在不容易，阻力很大，顾虑很多，很多无名恐惧，给要做这些事的人带了 很多帽子。改革开放，尤其是开放很不容易。一涉及到与外国人的关系，总怕丧权辱国，怕自己吃亏，说“肥水不流外人田”。所以我常给他们讲这个道理：外国人 到中国投资，他们本来就很多顾虑，我们的政策这样不稳定，应该说有很多风险，要怕的应该是拿钱进来的外商，我们中国政府有什么可怕的呢？<br />
