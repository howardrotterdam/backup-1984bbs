《大迁徙》 - 小说<br />
<br />
新京报9月1日报道 8月19日，55岁的谢朝平被陕西渭南警方从北京家中带走。带走时，警察口头留下一句“涉嫌非法经营”。 <br />
<br />
8月30日，谢朝平的律师周泽从警方获悉，“非法经营”是因为谢朝平自费出版了1万本纪实文学《大迁徙》，那是谢朝平三年的采访成果，记录了三门峡移民的一些历史遗留问题，渭南地区的移民是作品主角。 <br />
<br />
写书为何成为“非法经营”？8月30日，渭南市临渭区公安分局法制科负责人在电话里称“不便多说”。 <br />
<br />
抓捕 <br />
<br />
7名便衣警察入户抓人 <br />
<br />
今年6月辞去记者工作的谢朝平，租住在石景山区鲁谷东里社区。8月29日，其妻李琼说，8月19日下午5时许，有人敲门，自称是“人口普查的”，开门后她发现有7个穿便衣的人，确定租住者是谢朝平后，“他们冲上去擒住谢朝平，并戴上手铐。”李琼说，他们还念叨“找的就是谢朝平。” <br />
<br />
此后，对方出示了警察证，四人是陕西渭南的，还有三个陪同的北京警察。随后的两小时，警方对该住所进行搜查，带走了谢朝平的书稿、笔记本电脑、录音笔和U盘。在谢朝平的要求下，警察列了一个查抄清单，但清单并未留下。 <br />
<br />
李琼说，她让警察出示拘传手续，警察最后口头说了句“涉嫌非法经营”。当晚8时许，谢朝平被带走。 <br />
<br />
警方 <br />
<br />
“非法经营”与出书有关 <br />
<br />
8月30日，谢朝平的律师周泽前往渭南市临渭区公安分局法制科了解案情，答复是“涉嫌非法经营”，具体所指就是谢朝平今年5月出版的长篇纪实文学《大迁徙》。该书稿以上世纪50年代三门峡黄河大移民为背景，记录了那段历史。 <br />
<br />
谢朝平是四川平昌人，当过老师，后来通过考试到达县监察局工作，再后来调进达州市检察院，一直搞文字工作，曾写过多篇报告文学并获奖。 <br />
<br />
2005年，谢朝平办理提前离岗手续，应聘到《检察日报》下属《方圆》杂志社当记者。2006年，谢朝平从渭南移民局工会主席李万民处获悉了三门峡遗民的历史遗留问题，并开始关注此问题。李琼称，此后三年，谢朝平一有空就去陕西采访移民。采访的素材多了，谢朝平决定写书。 <br />
<br />
出书 <br />
<br />
采访三年自掏5万出书 <br />
<br />
今年年初，谢朝平写成了10万字的报告文学《移民》，后改名《大迁徙》，最终与《火花》杂志社下月刊编辑部谈妥，以该杂志2010年增刊的方式出版。 <br />
<br />
《火花》杂志由山西省文联主办，系全国百家社科核心期刊。杂志为半月刊，上半月的“大众文艺版”在太原由杂志社自身出版，下半月的“文化创意产业”在北京出版，承包给北京名镇方志文化传播中心运作，该中心每年交给杂志社15万元承包费。今年5月，谢朝平与《火花》北京编辑部谈成出版条件，谢支付5万印务费用，以增刊方式付印1万册。 <br />
<br />
说法 <br />
<br />
杂志社承认增刊不合程序 <br />
<br />
李万民称，付印时，谢朝平与其联系，打算“送”一些书给移民，并委托其在渭南城区找个地方存放。 <br />
<br />
李万民称，6月26日，书送到渭南。次日，渭南市文化稽查队以“杂志属非法出版物”为由，将其全部没收。与此同时，库区各县市政府还派出公安、乡镇干部和文化稽查队员从移民家里搜走《大迁徙》增刊。 <br />
<br />
渭南下辖华阴市的移民代表董生鑫称，其运回的4600多本《大迁徙》亦被华阴市文体局扣押。 <br />
<br />
<br />
此后，渭南市新闻出版部门和公安部门去山西，要求对书籍的合法性进行审查。 <br />
<br />
“他们先后找了省出版局和文联，闹得很厉害。”8月30日，《火花》杂志社社长王作忠称，出版部门最终发现此次增刊的不合程序之处。按照规定，出版单位无权擅自出版增刊，出增刊需逐级上报审批，但北京编辑部没有这么做。 <br />
<br />
《火花》杂志北京编辑部的执行社长魏丕植对此表示，“因为没有经验，所以没走审批程序。” <br />
<br />
7月24日，山西省新闻出版局通知《火花》杂志社，停止出版杂志的下半月刊。昨日，山西省新闻出版局报刊处负责人称，此事还在调查中。</div>

							
							
							
							
