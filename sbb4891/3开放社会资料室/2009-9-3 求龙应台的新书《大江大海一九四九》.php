													<h2>今夜星光灿烂―读龙应台《大江大海一九四九》及其他</h2>
						
						
																											<div id="postmessage_240805" class="t_msgfont">安裕周, 9月13日<br />
<br />
【明报专讯】掩卷之后，望着龙应台《大江大海一九四九》半蓝半红宋体字清劲秀丽的封面，我想，从白先勇《花桥荣记》的国文教师卢先生到陈映真《将军族》的三角眼，他们终于都找到了各自佚失却又一模一样的上半集人生。 <br />
<br />
是的，这就是一九四六年内战爆发，以迄一九四九年中共建政这三年之间的一段全面被遗忘的历史。这一段历史在中共政权和国民党在台湾小朝廷的威权政治之下，不是遭到全面扭曲便是刻意忘记。二○○九年是中共称为解放战争国民党唤作内战的结束六十周年，这一年也是台湾找到一己身分（identity）的关键年份――龙应台这部新作想必会令中共讶然惊觉，六十年的意识形态天堑相隔，台湾已然成为中国（middle kingdom）以外的他者（other）。 <br />
<br />
回首前尘，内战不啻是中国人民的悲哀。一九四六年，刚从八年抗战的废墟颠颠巍巍准备重新站起来的中国再次笼罩在战争阴影里，蒋介石毛泽东谈不拢，和平协定撕毁，内战全面爆发。这场在战场上恶战三年、却在两岸人民心目中延绵六十年的战争，在中共历史上扮演了极其主要的角色――毛泽东运筹帷幄决胜于千里之外，第四野战军林彪在东北华北连打两场举世瞩目的辽沈战役和平津战役，全歼百万国军，抗日名将陈明仁傅作义尽成战犯；刘伯承邓小平的第二野战军在江南发动淮海战役，歼灭黄维兵团等五十五万人，司令杜聿明被俘。要一提的是杜聿明有一女杜致礼，女婿是诺贝尔物理学奖得奖者杨振宁。 <br />
<br />
蒋介石虽是日本士官学校出身，但那时国民党气数已尽，三上五下二给小米加步枪的共军打得全军尽墨，加上国军内部倾轧，蒋介石本拟靠桂系的白崇禧在武汉一线阻击林彪部队，但在国军内有小诸葛之称的白崇禧，眼看林彪如狼似虎杀到，虚晃一招西逃回广西。林彪本来准备与白崇禧主力部队恶战八十回合，结果军心散涣的国军没有凑兴，林彪部队直插华南，解放广东后渡海攻海南岛，完成大业。 <br />
<br />
在世界战争史上，像国军那样的三年间败掉九百六十万平方里的家与国固然罕见，像共军那样摧枯拉朽一口吞下整个国家更是匪夷所思。如此的成王败寇出来的历史必然是有着截然不同的视角，中共的内战史除了四支野战军如何英勇善战每战必捷，便是老百姓的「箪食壶浆，以迎王师」。当然，话语权在手的中共内战史是片面的，内地读者如果能够读到《大江大海一九四九》的话，也许会感慨被蒙骗了几十年，原来共军并非百战百胜――东北战场，国军将领陈明仁曾经予共军重创，《大江大海一九四九》第三部二十五节〈走一趟吉林路〉s「林彪的东北野战军用四个师围攻，……激烈战斗了一个礼拜，共军败退而走。……陈明仁巡视战地，看着敌人尸体也不禁流下眼泪」；师长潘裕昆走在尸阵，「只沙哑地说了一句话s『一将功成万骨枯』，眼睛就红了」。 <br />
<br />
<strong>败仗遭掩盖胜利亦扭曲 </strong><br />
<br />
不仅败仗的历史遭到掩盖，连胜战也因着这样那样的原因受到各式各样扭曲。一九七一年，林彪外逃内蒙坠机身亡，中共内战史出现更大面积的空白。中共建政后分封十大元帅，排行第一和第二分别是朱德和彭德怀，这是井冈山以降的历史因素使然，朱彭早在那年代已奠定在军队的地位，林彪则以赫赫战功排第三。这是一个恰当的安排，共军三大解放战役，林彪独力打其二，在共军内有战神之称，打天津一役，林彪甚至不必出马，仅派遣参谋长刘亚楼便解决碉堡如林的死城天津。可是，林彪坠机，中共战史从此缺了辽沈战役和平津战役细节，十大元帅究竟姓甚名谁也模模糊糊，甚至到了前些年，官方的十大元帅说法竟是「朱德、彭德怀、刘伯承、贺龙、陈毅、罗荣桓、徐向前、聂荣臻、叶剑英等」――堂堂开国元帅林彪，在政治正确压倒一切的氛围里，居然矮化得变成一个「等」字。 <br />
<br />
夹着尾巴到台湾的蒋介石也不见得比中共开明，一切都有规有矩跳不出法统，国军被共军打得无处可逃，大军挤上军舰货轮渔船逃到台湾，一直自认正朔的国民党竟还不怕脸红把这叫作「播迁」。播迁源出北周《哀江南赋》，「彼凌江而建国，始播迁于吾祖」，说到底原来是一桩好事哩。到台北后的蒋介石草山静思，毋忘在莒，然而作为职业军人的最痛便是丢城失地，何况这次是失去整整一个国家！台湾局势五十年代在美国核子伞保护下逐渐稳定，文学创作渐见丰富，可是一九四六年后战场上的马革裹尸节节败退、四九年撤走一刻的「最是仓皇辞庙日」 ，都在各式压力下只在亲友间口耳相传或永藏心底，独独难以见诸文字。 <br />
<br />
台湾的文字工作者在这空间下也不尽是从此刀枪入库马放南山，他们在各自的人生历练里找到题材，白崇禧之子白先勇在《台北人》十四个短篇里，勾勒的是来台故人对故里的怀忆，这都是时不予我的人生悲剧。 《花桥荣记》的国文教师卢先生，三十出头，「卢老太爷从前在湖南做过道台，是我们桂林有名的大善人」。这一段只有下集台湾故事的断代史缺了上集的大陆篇，卢先生以前在大陆到底做过些什么，只是一句断然回答「大陆撤退，我们自己军队一把火，都烧光喽」。撤退也者，崩败也；卢老太爷「从前」在湖南做事，「从前」是哪年哪月，也是一带而过。 《冬夜》里，余吕诮淌谠诶溆甓夜为海外学人的当年北平同学吴柱国接风，说到吴出国第二年「北平便陷落了」。字里行间人们读到另一种况味s那是含蓄隐晦的大陆败退仓皇来台，至于是如何败退如何仓皇如何从「旧时王谢堂前燕」变成「飞入寻常百姓家」，在几乎只能姓蒋的才能当总统的台湾，那时是一种禁忌，尤其是对一九四九年以前的种种，虽然史实早就流传多时。 <br />
<br />
<strong>只得下半集的台湾故事 </strong><br />
<br />
白先勇在《台北人》的扉页里是这样写的s「纪念先父母以及他们那个忧患重重的时代」，诚然这是桂系主事人之子的纪念，但不独白先勇如此，台湾社会的低下层，也有他们的只得下半集的台湾故事。陈映真《将军族》的三角眼便是其一，在有着强烈小林多喜二风格的短篇，三角眼「在沦陷给日本的东北，他的姐姐曾说给他听过一些故事」。这是我读过的台湾一九四九年后文学作品里，大陆原乡直线距离台湾最远的一人s东北，比起卢先生的桂林、余吕诘谋逼健《永远的尹雪艳》的上海都远，而且都偏偏缺了在大陆的回忆，这是六十年代台湾文学的一个共通点。 <br />
<br />
内战不提，大陆旧史佚失，这是人为使然。这既有中共的战胜史观，也有国民党败退大陆的失败史观，两种其实都有欠周全，但这里面夹杂的却不纯是威权主义的牵涉，而是民族主义者面对故国河山的难舍。 <br />
<br />
蒋介石的中国近代史观便是他的个人史观，毛泽东曾自称是民族主义者多于共产主义者，蒋介石除了跟随宋美龄信膺基督教，他这个出身浙江的军人也是民族主义者。一九四九年后的中共史观，蒋介石被描述为「卖国贼」，经过岁月爬梳，中共近年开始为蒋介石洗去污名，甚至实事求是指出国军才是抗日战争的正面战场主力，一洗是八路军打倒日本侵略者的中共史观。这其中夹缠了对台湾陈水扁治下的蓝营统战，客观上也是在「民族大义」大纛下的拉拢。中共最近几年改弦易帜，是看到台湾开始向太平洋东飘，逐渐远离传统认知里的「中国」（middle kingdom），这才加大力度，企图从历史认知上扭转这一趋势。 <br />
<br />
蒋介石在当权年代怯于承认内战惨败，除了是职业军人的天性，更多的是他拒绝从「中国」的认知中脱离出来。蒋介石心目中，历史或文学作品提及国民党内战大败或丢失大陆，是对他终有一天重回故土的否定s他不可能以一个败军之将的身分回到大陆，内战溃败的历史、几百万人仓卒迁台，统统会令他重回大陆时失去国之主人的统治合法性（legitimacy）。蒋介石这种拒绝承认永远留在台湾的心理，在生之年如是，甚至去世后亦如是，不但在遗嘱再提「光复大陆国土，复兴民族文化，坚守民主阵容，为余毕生之志事」，更不忘浙江奉化老家，灵柩暂厝慈湖，准备重回中原。 <br />
<br />
蒋介石不提内战，中共却大张旗鼓大谈特谈内战，其实是与蒋介石隔江唱和，借内战维持中国大陆――台湾的血缘和法统关系。一九五八年，解放军突然对金门发动炮轰，史称金门炮战，这场世界战争史罕见的奇特炮战，炮弹只打空地不打人，而且后来发展至大陆台湾隔日互轰，政治态度溢于言表。若干年后，中共史料透露，这是毛蒋向美国总统艾森豪威尔表态s金门是国民党政权的前沿，国军绝不退出，更大涵意在于，因为只要撤出金门，台湾即断绝与中国大陆的血脉，为美日推动或逼使台湾独立创造条件。 <br />
<br />
蒋介石毛泽东墓木已拱逾三十年，这一默契到了下一代领导人已是无人接棒，加上大陆和台湾发展各走一边，台湾大步流星走向民主化及本土化，中共则祭出先吃饱饭为先的人权论，价值取向大相径庭，六十年的意识形态天堑，台湾逐渐孕育出非中原甚至非中国（non-china）的集体认知。北京天安门广场不乏兴高采烈的年轻台湾游客，他们是从台湾到「中国」游览，于这批成长于八十年代、目睹电视直播June Fourth镇压的年轻一代而言，中共治下的中国，是一个不在他们的「国家」认知里的他者（other）――他们兴许会为June Fourth事件哀悼，但不会认为倒在广场上的学生是骨肉兄弟，因为那毕竟是另一个国家。 <br />
<br />
<strong>政治割裂We don't give a damn </strong><br />
<br />
这种还原历史但暗示政治割裂的情况近年尤为明显，大陆电影《集结号》台湾上演，主角张涵予一举夺得金马影帝，这是非常明显的信号――《集结号》内容是内战年代共军打败国军，过往不可能会在台湾上映，但这次不但上映而且还夺得影帝，折射出来的政治潜台词是台湾民众对国共之争的we don't give a damn态度，认定这些是父执辈以至祖父辈的历史，与我何干。作为一个爱书人，我会视龙应台的《大江大海一九四九》是对台湾内战文学里一九四六年到一九四九年的整整一大块刻意遗忘的补遗，也是对六十年来中共史观下的所谓「解放战争历史」的蓄意扭曲的正面调整；作为对中国近代史兴致浓厚的其中一人，我感到《大江大海一九四九》补足了内战颠沛流离的人文感觉，更提供了相对较全面的内战视觉，大大丰富了这一段早已在我们记忆里残缺不全的历史。 <br />
<br />
六十年的迂回和轮回之后，最终是历史的归历史，文学的归文学；当然，政治的也归政治，于一介草民而言，这从来不是我关心或者担心的，那是政客的事， I don't give a damn。</div>

							
							
							
							
