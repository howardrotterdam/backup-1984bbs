　　本月一日，龙应台在北京大学百年讲堂发表演讲“文明的力量：从乡愁到美丽岛”。前一天同一地点，她刚从深受大陆知识份子推崇的报纸“南方周末”手中，接下“二○一○中国梦践行者”奖杯。<br />
<br />
　　龙应台这次在北大演讲，吸引超过千名听众。龙应台在演讲中回应“南方周末”请她谈“中国梦”的要求，侃侃而谈一九四九之后，台湾人面对“中国梦”的破灭与转折，最后期待中国以文明大国的形象崛起于世界舞台。<br />
<br />
　　上周四南方周末以删节方式刊出龙应台演讲内容，引起华文读者上网寻找演讲全文。龙应台得以“解禁”在大陆公开演讲，演讲内容谈及“美丽岛事件”等敏感议题却未遭官方封杀，深具意义。联合报获龙应台同意，今天刊出演讲全文，以飨读者。<br />
<br />
　　我们的“中国梦”<br />
<br />
　　第一次接到电话，希望我谈谈“中国梦”的时候，我的第一个反应是：“一千枚飞弹对准我家，我哪里还有中国梦啊？”<br />
<br />
　　可是沉静下来思索，一九五二年生在台湾的我，还有我前后几代人，还真的是在“中国梦”里长大的，我的第一个中国梦是什么呢？<br />
<br />
　　我们上幼稚园时，就已经穿着军人的制服、带着木制的步枪去杀“共匪”了，口里唱着歌。当年所有的孩子都会唱的那首歌，叫做《反攻大陆去》：<br />
<br />
　　反攻　反攻　反攻大陆去<br />
<br />
　　大陆是我们的国土<br />
<br />
　　大陆是我们的疆域<br />
<br />
　　我们的国土　我们的疆域<br />
<br />
　　不能让共匪尽着盘据<br />
<br />
　　不能让俄寇尽着欺侮<br />
<br />
　　我们要反攻回去　我们要反攻回去<br />
<br />
　　反攻回去　反攻回去<br />
<br />
　　把大陆收复 把大陆收复<br />
<br />
　　这不是一种“中国梦”吗？这个梦其实持续了满久，它是一个至高无上的图腾，也被人们真诚地相信。<br />
<br />
　　仓皇的五十年代进入六十年代，“中国梦”持续地深化。余光中那首《乡愁四韵》传颂一时：<br />
<br />
　　给我一瓢长江水啊长江水<br />
<br />
　　那酒一样的长江水<br />
<br />
　　那醉酒的滋味是乡愁的滋味<br />
<br />
　　给我一瓢长江水啊长江水<br />
<br />
　　给我一掌海棠红啊海棠红<br />
<br />
　　那血一样的海棠红<br />
<br />
　　那沸血的烧痛是乡愁的烧痛<br />
<br />
　　给我一掌海棠红啊海棠红<br />
<br />
　　一九四九年，近两百万人突然之间被残酷的内战连根拔起，丢到了一个从来没有去过、甚至很多人没有听说过的海岛上。在战火中离乡背井，颠沛流离到了岛上的人，思乡之情刻骨铭心，也是无比真诚的。那分对中华故土的魂牵梦绕，不是“中国梦”吗？<br />
<br />
　　梦的基座是价值观<br />
<br />
　　我的父母那代人在一种“悲愤”的情结中挣扎着，我这代人在他们乡愁的国家想像中成长。但是支撑着这个巨大的国家想像下面，有一个基座，垫着你、支撑着你，那个基座就是价值的基座。<br />
<br />
　　它的核心是什么？台湾所有的小学，你一进校门门当头就是四个大字：“礼义廉耻”。进入教室，简朴的教室里面，墙壁上也是四个大字：“礼义廉耻”。如果一定要我在成千上万的“格言”里找出那个最基本的价值的基座，大概就是这四个字。<br />
<br />
　　小的时候跟大陆一样，四周都是标语，只是内容跟大陆的标语不一样。最常见到的就是小学里对孩子的解释：<br />
<br />
　　礼，规规矩矩的态度。<br />
<br />
　　义，正正当当的行为。<br />
<br />
　　廉，清清白白的辨别。<br />
<br />
　　耻，切切实实的觉悟。<br />
<br />
　　上了初中，会读文言文了，另一番解释就来了：<br />
<br />
　　礼义廉耻，国之四维，四维不张，国乃灭亡。～管仲<br />
<br />
　　然而四者之中，耻尤为要。人之不廉而至于悖礼犯义，其原皆生于无耻也。故士大夫之耻，是为国耻。～顾炎武<br />
<br />
　　“士大夫之耻，是为国耻”，这些价值在我们小小的心灵有极深的烙印。<br />
<br />
　　二○○六年，上百万的“红衫军”包围总统府要求陈水扁下台，台北的夜空飘着大气球，一个一个气球上面分别写着大字：“礼”，“义”，“廉”，“耻”。我到广场上去，抬头乍看这四个字，感觉好像是全台湾的人到这广场上来开小学同学会了。看着那四个字，每个人心领神会，心中清晰知道，这个社会在乎的是什么。<br />
<br />
　　除了价值基座，还有一个基本的“态度”。我们年纪非常小，可是被教导得志气非常大，小小年纪就已经被灌输要把自己看成“士”，十岁的孩子都觉得自己将来就是那个“士”。“士”，是干什么的？<br />
<br />
　　士不可以不弘毅，任重而道远。仁以为己任，不亦重乎？死而后已，不亦远乎？～论语泰伯篇<br />
<br />
　　我初中一年级的国文老师叫林弘毅，数学老师叫陈弘毅。同时期大陆很多孩子可能叫“爱国”、“建国”，我们有很多孩子叫“弘毅”。我们都是要“弘毅”的。<br />
<br />
　　对自己要期许为“士”，对国家，态度就是“以国家兴亡为己任，置个人生死于度外”。这是蒋介石的名言，我们要背诵。十一二岁的孩子背诵这样的句子，用今天的眼光看，挺可怕的，就是要你为国家去死。<br />
<br />
　　然而在“国家”之上，还有一句：<br />
<br />
　　为天地立心，为生民立命，为往圣继绝学，为万世开太平。～张载<br />
<br />
　　对那么小的孩子也有这样的期待，气魄大得有点吓人。饶有深意的是，虽然说以国家至上，但是事实上张载所说的是，在“国家”之上还有“天地”，还有“生民”，它其实又修正了国家至上的秩序，因为“天地”跟“生民”比国家还大。<br />
<br />
　　十四岁的时候，我第一次读到《国语》，《国语》是两千多年前的经典了，其中一篇让我心里很震动：<br />
<br />
　　厉王虐，国人谤王。召公告曰：“民不堪命矣！”王怒，得卫巫，使监谤者。以告，则杀之。国人莫敢言，道路以目。王喜，告召公曰：“吾能弭谤矣，乃不敢言。”召公曰：“是障之也。防民之口，甚于防川，川壅而溃，伤人必多。民亦如之…”<br />
<br />
　　王不听，于是国人莫敢出言。三年，乃流王于彘。<br />
<br />
　　最后一句，简单几个字，却雷霆万钧，给十四岁的我，深深的震撼。<br />
<br />
　　就是这个价值系统，形成一个强固的基座，撑起一个“中华大梦”。<br />
<br />
　　我是谁？<br />
<br />
　　这个中国梦在一九七○年代出现了质变。<br />
<br />
　　一九七一年中华民国被迫退出联合国，台湾人突然之间觉得自己变成了孤儿。可是，最坏的还没到，一九七九年一月一号，中美正式断交，这个“中”指的是当时的中华民国，也就是台美断交，中美建交。长期被视为“保护伞”的美国撤了，给台湾人非常大的震撼，觉得风雨飘摇，这个岛是不是快沉了。在一种被整个世界抛弃了而强敌当前的恐惧之下，救亡图存的情感反而更强烈，也就在这个背景下，原来那个中国梦对于一部分人而言是被强化了，因为危机感带来更深更强的、要求团结凝聚的民族情感；大陆人很熟悉的《龙的传人》，是在那样的悲愤伤感的背景下写成的。这首歌人人传唱，但是一九八三年，创作者“投匪”了，歌，在台湾就被禁掉了，反而在大陆传唱起来，情境一变，歌的意涵又有了转换。<br />
<br />
　　你们是否知道余光中《乡愁》诗里所说的“海棠红”是什么意思？<br />
<br />
　　地图<br />
<br />
　　图／取自龙应台讲稿<br />
<br />
　　我们从小长大，那个“中国梦”的形状，也就是中华民国的地图，包含外蒙古，正是海棠叶的形状。习惯这样的图腾，开始看见中华人民共和国地图的前面好几年，我都还有种奇怪的错觉，以为，哎呀，这中国地图是不是画错了？<br />
<br />
　　一九七○年代整个国际情势改变，台湾的“中国梦”开始有分歧。对于一部分人而言，那个“海棠”中国梦还虔诚地持续着，可是对于另外一部分人就不一样了。<br />
<br />
　　梦，跟着身边眼前的现实，是会变化的，一九四九年被连根拔起丢到海岛上的一些人，我的父母辈，这时已经在台湾生活了三十年，孩子也生在台湾了―这海岛曾是自己的“异乡”却是孩子的“故乡”了，随着时间推移，无形之中对脚下所踩的土地产生了具体而实在的情感。所以，你们熟悉余光中先生写的那首《乡愁》，却可能不会知道他在一九七二年的时候创作了另外一首诗，诗歌礼赞的，是台湾南部屏东海边一个小镇，叫枋寮：<br />
<br />
　　车过枋寮<br />
<br />
　　雨落在屏东的甘蔗田里<br />
<br />
　　甜甜的甘蔗　甜甜的雨<br />
<br />
　　从此地到山麓 一大幅平原举起<br />
<br />
　　多少甘蔗，多少甘美的希冀<br />
<br />
　　长途车驶过青青的平原<br />
<br />
　　检阅牧神青青的仪队<br />
<br />
　　余先生这首诗，有“中国梦”转换的象征意义。但是今天想跟大家分享的，还有一首我称之为“里程碑”的歌，叫《美丽岛》。<br />
<br />
　　一位淡江大学的年轻人，李双泽，跟很多台湾年轻人一样， 七○年代发现台湾不能代表中国，而且逐渐被国际推到边缘，在危机感和孤独感中，年轻人开始检视自己：为什么我们从小被教要爱长江、爱黄河、歌颂长城的伟大 ―─那都是我眼睛没见过，脚板没踩过的土地，而我住在淡水河边，怎么就从来不唱淡水河，怎么我们就不知道自己村子里头小山小河的名字？台湾也不是没有大江大海呀？<br />
<br />
　　演讲现场播放了几首相关歌曲。<br />
<br />
　　图／南方周末提供<br />
<br />
　　青年人开始推动“唱我们的歌”，开始自己写歌。那个“中国梦”显得那么虚无飘渺，是不是该看看脚下踩的泥土是什么样？他写了《美丽岛》，改编于一首诗，一下子就流行起来，大家都喜欢唱。<br />
<br />
　　《美丽岛》真的是代表了从中国梦慢慢地转型到“站在这片泥土上看见什么、想什么”的“台湾梦”里程碑：<br />
<br />
　　我们摇篮的美丽岛<br />
<br />
　　是母亲温暖的怀抱<br />
<br />
　　骄傲的祖先正视着<br />
<br />
　　正视着我们的脚步<br />
<br />
　　他们一再重覆地叮咛<br />
<br />
　　不要忘记　不要忘记<br />
<br />
　　他们一再重覆地叮咛<br />
<br />
　　荜路蓝缕以启山林<br />
<br />
　　婆娑无边的太平洋<br />
<br />
　　怀抱着自由的土地<br />
<br />
　　温暖的阳光照耀着<br />
<br />
　　照耀着高山和田园<br />
<br />
　　我们这里有勇敢的人民<br />
<br />
　　荜路蓝缕以启山林<br />
<br />
　　我们这里有无穷的生命<br />
<br />
　　水牛　稻米　香蕉　玉兰花<br />
<br />
　　一九七五年，我二十三岁，到美国去读书，每天泡在图书馆里，从早上八点到半夜踩着雪光回到家，除了功课之外就有机会去读一些中国近代史的书，第一次读到国共内战的部分，第一次知道一九二七年国民党对共产党员的杀戮，才知道之前所接受的教育那么多都是被党和国家机器所操纵的谎言，这是一个很大的震撼。十年之后写了《野火集》，去“腐蚀”那个谎言。<br />
<br />
　　一九七九年，我个人的“中国梦”也起了质变。在中国梦笼罩的台湾，我们是讲“祖籍”的。也就是说，任何人问，龙应台你是哪里人，我理所当然的回答就是：“我是湖南人。”<br />
<br />
　　这么一路做“湖南人”做了几十年，到一九七九年，中国大陆开放了，我终于在纽约生平第一次见到了一个真正的“共匪”站在我面前，这个朴实人刚刚从湖南出来，一口浓重的湖南腔。有人冲着他问“你是哪里人”，他就说“我是湖南人”，问话者接着就回头问我“你是哪里人”――我就愣住了。<br />
<br />
　　我不会说湖南话，没有去过湖南，对湖南一无所知，老乡站在面前，我登时就说不出话来了。这一辈子的那个“中国梦”突然就把我懵在那儿了，这是一九七九年一个非常大的震撼――原来啊，我是台湾人。<br />
<br />
　　一起做梦，一起上课<br />
<br />
　　从海棠叶的大中国梦慢慢过渡到台湾人脚踩着泥土的小小台湾梦，人民在七○年代末八○年代初开始问“我是谁”。八○年代后，台湾两千多万人走向了转型，自我感觉就是越来越小，什么事情都一步一个脚印，一点一点做。所以，台湾人就一块儿从大梦慢慢转到小梦的路上来了，开始一起上八○年代的民主大课。这个民主课程上得有够辛苦。<br />
<br />
　　《美丽岛》这首歌，在一九七九变成党外异议人士的杂志名字，集结反对势力。当年十二月十日，政府对反对者的大逮捕行动开始，接着是大审判。面临巨大的挑战，国民党决定审判公开，这是审判庭上的一张照片：<br />
<br />
　　美丽岛大审，第二排露出一排白牙笑得潇洒的是施明德，施明德的右边是陈菊，左边是吕秀莲。<br />
<br />
　　图／中央社资料照片<br />
<br />
　　你们认得其中任何一个人吗？第二排露出一排白牙笑得潇洒的，是施明德，他被判处无期徒刑。施明德右手边的女子是陈菊，今天的高雄市长，左手边是吕秀莲，上一任的副总统。<br />
<br />
　　我想用这张图片来表达八○年代台湾人慢慢地脚踩泥土重建梦想和希望的过程。如果把过去的发展切出一个三十年的时间切片来看，刚好看到一个完整的过程：这图里有三种人，第一种是叛乱犯，包括施明德，吕秀莲，陈菊等等，她们俩分别被判十二年徒刑；第二种是英雄，在那个恐怖的时代，敢为这些政治犯辩护的律师，包括陈水扁，谢长廷，苏贞昌等等；第三类是掌权者，当时的总统是蒋经国先生，新闻局长是宋楚瑜先生。从这些名字你就看出，在三十年的切片里，政治犯上台变成了掌权者，掌权者下台变成了反对者，而当时得尽掌声以及人们殷殷期待的，以道德作为注册商标的那些英雄们变成了什么？其中一部分人变成了道德彻底破产的贪污嫌疑犯。<br />
<br />
　　这个转变够不够大？亲眼目睹这样一个切肤痛苦的过程，你或许对台湾民主的所谓“乱”有新的理解。<br />
<br />
　　它所有的“乱”，在我个人眼中看来，都是民主的必修课；它所有的“跌倒”都是必须的实践，因为只有真正跌倒了，你才真正地知道，要怎么再站起来，跌倒本身就是一种考试。所以，容许我这样说：台湾民主的“乱”，不是乱，它是必上的课。<br />
<br />
　　表面上台湾被撕裂得很严重，但不要被这个表面骗了。回到基座上的价值观来看，从前的中国梦慢慢被抛弃了，逐渐发展为台湾的小梦，然后一起上非常艰辛、痛苦的民主课，然而台湾不管是蓝是绿，其实有一个非常结实的共识，比如说：<br />
<br />
　　国家是会说谎的，<br />
<br />
　　掌权者是会腐败的，<br />
<br />
　　反对者是会堕落，<br />
<br />
　　政治权力不是唯一的压迫来源，<br />
<br />
　　资本也可能一样的压迫。<br />
<br />
　　而正因为权力的侵蚀无所不在，所以个人的权利、比如言论的自由，是每个人都要随时随地、寸土必争、绝不退让的。<br />
<br />
　　这是大多数台湾人的共识。你所看到的争议、吵架，立法院撕头发丢茶杯打架，其实都是站在这个基础上的。这个基础，是以共同的价值观建立起来的。<br />
<br />
　　我有中国梦吗？<br />
<br />
　　回到今天中国梦的主题，可能有很多台湾人会跳起来说：中国不是我的梦，我的梦里没有中国。<br />
<br />
　　但是，你如果问龙应台有没有中国梦，我会先问你那个中国梦的“中国”指的是什么？如果指的是“国家”或“政府”，“国家”“政府”在我心目中不过就是个管理组织，对不起，我对 “国家”没有梦，“政府”是会说谎的。但如果你说的“中国”指的是这块土地上的人，这个社会，我怎么会没有梦呢？别说这片美丽的土地是我挚爱的父亲、母亲永远的故乡，这个地方的好跟坏，对于台湾有那么大的影响，这个地方的福与祸，会牵动整个人类社区的未来，我怎会没有中国梦呢？<br />
<br />
　　我们就从“大国崛起”这个词说起吧。我很愿意看到中国的崛起，可是我希望它是以文明的力量来崛起的。<br />
<br />
　　如何衡量文明？我愿意跟大家分享我自己衡量文明的一把尺。它不太难。看一个城市的文明的程度，就看这个城市怎样对待它的精神病人，它对于残障者的服务做到什么地步，它对鳏寡孤独的照顾到什么程度，它怎样对待所谓的盲流民工底层人民。对我而言，这是非常具体的文明的尺度。<br />
<br />
　　一个国家文明到哪里，我看这个国家怎么对待外来移民，怎么对待它的少数族群。我观察这个国家的多数如何对待它的少数――这当然也包含十三亿人如何对待两千三百万人！<br />
<br />
　　谁在乎“大国崛起”？至少我不在乎。我在乎的是刚才我所说的文明刻度――你这大国怎么对待你的弱势与少数，你怎么包容意见不同的异议份子，这，才是我在乎的。如果说，所谓的大国崛起，它的人民所引以自豪的，是军事的耀武扬威，经济的财大气粗，政治势力的唯我独尊，那我宁可它不崛起，因为这种性质的崛起，很可能最终为它自己的人民以及人类社区带来灾难和危险。<br />
<br />
　　谁又在乎“血浓于水”？至少我不那么在乎。如果我们对于文明的尺度完全没有共识，如果我们在基座的价值上，根本无法对话，“血浓于水”有意义吗？<br />
<br />
　　我的父亲十五岁那年，用一根扁担、两个竹篓走到湖南衡山的火车站前买蔬菜，准备挑回山上。刚巧国民党在招宪兵学生队，这个少年当下就做了决定：他放下扁担就跟着军队走了。<br />
<br />
　　我的父亲在一九一九年出生，二○○四年，我捧着父亲的骨灰回到了湖南衡山龙家院的山沟沟，乡亲点起一路的鞭炮迎接这个离家七十年、颠沛流离一生的游子回乡。<br />
<br />
　　在家祭时，我听到一个长辈用最古老的楚国乡音唱出凄切的挽歌。一直忍者眼泪的我，那时再也忍不住了。<br />
<br />
　　楚国乡音使我更深刻地认识到父亲一辈子是怎么被迫脱离了他自己的文化，过着不由自主的放逐的一生。一直到捧着他的骨灰回到那片土地，我才深切的感觉到这个七十年之后以骨灰回来的少年经历了怎样的中国的近代史。而我在浙江新安江畔长大的母亲，是如何地一生怀念那条清澈见鱼的江水。<br />
<br />
　　一个开阔、包容的中国<br />
<br />
　　所以，请相信我，我对中国的希望是真诚的。但是请不要跟我谈“大国崛起”， 请不要跟我谈“血浓于水”，我深深盼望见到的，是一个敢用文明尺度来检验自己的中国；这样的中国，因为自信，所以开阔，因为开阔，所以包容，因为包容，所以它的力量更柔韧、更长远。当它文明的力量柔韧长远的时候，它对整个人类的和平都会有关键的贡献。<br />
<br />
　　一九八五年我写《野火集》，一九八六年一月，《野火集》在风声鹤唳中出版。八月，我迁居欧洲。离开台湾前夕，做了一场临别演讲，是“野火”时期唯一的一次。演讲在害怕随时“断电”的气氛中进行。今天，二○一○年八月一日，在北京大学，我想念那篇演讲的最后一段，与大陆的读者分享：<br />
<br />
　　在临别的今天晚上，你或许要问我对台湾有什么样的梦想？<br />
<br />
　　有。<br />
<br />
　　今天晚上站在这里说话，我心里怀着深深的恐惧，恐惧今晚的言词带来什么后果，我的梦想是，希望中国人的下一代可以在任何一个晚上站在任何一个地方说出心里想说的话，而心中没有任何恐惧。我们这一代人所做的种种努力也不过是希望我们的下一代将来会有免于恐惧的自由。<br />
<br />
　　那是一九八六年八月十一日。<br />
<br />
　　（二○一○年八月一日北京大学百年纪念讲堂演讲全文）</div>

							
							
							
							
