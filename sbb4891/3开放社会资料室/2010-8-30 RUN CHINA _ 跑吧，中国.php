　　 <br />
　　不止一个人说出了胡志明的名字。看来对于美国民众而言，越战的影响力远大于一个有着古老文明并正走在高速发展道路上的东方大国。 <br />
　　 <br />
　　毛泽东或毛主席。说出正确答案的人约有一半。还有人指出，毛泽东影响了整个六十年代的美国学生。 <br />
　　 <br />
　　关于第二个问题，回答的就五花八门了。 <br />
