　　Rick Curnutt，1974年生于美国加州，18岁离开故乡去美国东部求学，21岁漂洋过海来到北师大学习汉语，22岁起在中国教授英语，课余以拍片为乐。在中国的十年间，Curnutt强烈的感受到了中国文化的博大丰富，以及东西方在生活、观念、文化等方面的巨大差异，于是他选择了中西方在文化、观念领域的差异、认知、交流、冲突作为自己拍摄的题材。 <br />
