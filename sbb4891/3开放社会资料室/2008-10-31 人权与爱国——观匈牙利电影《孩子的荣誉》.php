													<h2>人权与爱国――观匈牙利电影《孩子的荣誉》</h2>
						
						
																											<div id="postmessage_3013" class="t_msgfont">朱欣欣&nbsp;&nbsp;<br />
国家水球队的明星队员克尔西，享有令人羡慕的地位、荣誉、待遇，然而他却一步步成为政治叛逆者……这就是银幕上1956年匈牙利革命中一个人的命运。<br />
<br />
二战后，斯大林的独裁魔爪伸出了国界，包括匈牙利在内的东欧诸国成了苏联的仆从，斯大林死后，1956年的匈牙利革命和此前波兰的波兹南事件、此后1968年捷克斯洛伐克的布拉格之春等，都是共产主义阵营的几次强烈地震，震源皆来自民众对苏联和本国独裁者双重奴役的反抗。日本NHK公司2005年曾播出纪录片《匈牙利事件真相》。2006年匈牙利革命50周年之际，匈牙利电影人以史诗巨作《孩子的荣誉》作为纪念，再现了那场革命的血与火，让后人重新聆听先辈的心声。<br />
<br />
1956年的匈牙利革命具有争取民主自由与国家独立的双重意义。《孩子的荣誉》的叙事以一个人的觉醒过程为主线，将他的命运与这场革命交织在一起。<br />
<br />
作为影片主角的国家水球队队员克尔西和他的队友，除了每天在两个秘密警察监视下训练，准备参加奥运会，本来对政治不感兴趣，可是，在极权的国度，一切都是维持特权集团利益“合法性”的利用资源。正如中共《新华日报》1945年9月11日英国普里斯莱《让人民思想，民主最大的危机就在你的冷淡》一文中指出的：“你可以对政治毫无兴趣，可你必须确信：政治对你却太有兴趣，政治绝不会因为你对政治不关心而放过你。所以，假如你不想作戏台上一具可怜的傀儡，给别人随心所欲地推来推去，你就别无选择――必须关心政治。这其实就是关心你自己。”正是因为“政治”，在苏联进行的比赛中，克尔西他们有实力也不能赢球，必须维护“老大哥”的威望和彼此地位的“友谊”，面对裁判的不公、吹黑哨和苏联对手的人身攻击，他们只能屈辱地接受“失败”，连以言语和拳头自卫，都会遭到总理“菲尔大叔”的警告、威胁，或被秘密警察惩罚殴打。当个人权利和尊严无法得到保障，“爱国”就成了“接受奴役”的代名词，真正的爱国就无从谈起，剩下的只能是美化和维护特权集团的“爱国秀”,如同我们民族从小就被灌输掺杂使假的“爱国”毒汁，充当“爱国”玩偶或牺牲。还记得1980年代被官方封杀的电影《苦恋》里那个文革中被迫害的画家吗？“我爱祖国，可祖国不爱我。”他临终前爬行在雪地上，留下一个大大的问号雪印！所以克尔西的队友迪比说起被三个秘密警察殴打时骂道：“去它的社会主义平等论！”<br />
<br />
尽管这样，对于坐稳了“高等奴隶”地位的克尔西来说，感觉比同胞优越的他要求不高，“有水球和姑娘就够了。”他因为找朋友，偶然来到大学的独立学生联盟成立会场，看到同龄人在为自由独立而热血沸腾，听到要求言论自由、苏军撤离、赦免和释放政治犯等呼吁，可这些都不如他眼中的女学生领袖薇绮令他心动。当他训练后返家途中，到游行队伍里追逐薇绮时，却不敢接过她游行的旗帜，难怪薇绮讥讽他：“你吓破了胆，因为秘密警察正从屋顶往下拍照。”“你根本是个局外人，回去打你的水球吧！”后来，当克尔西举起旗帜加入游行，被同胞们的激情所感染，体会到争取自由的神圣感，这时他的主体意识才开始觉醒，将自己的自由与国家的命运对接。对克尔西来说，没有自由，他甚至无法拥有一场公平的比赛，无法实现一个运动员的价值，无法赢得属于他和祖国的荣誉。此时这个“国家”在他心中，不再是被特权集团的利益和意识形态捆绑的那个国家，不再是被他们别有用心地置于民意之上的偶像崇拜，而是符合人性和民意的理想中的“国家”，这时“爱国”才由于个体解放的诉求具有了真正的意义，因此“爱国”就是改造所爱之国，让它服膺于国民的意志和幸福。当年鲁迅先生说：“我的爱护中华民国，焦唇敝舌，恐其衰微，大半正是为了使我们得有剪辫的自由。”胡适先生对青年们说过：“争你们个人的自由，便是为国家争自由！争你们自己的人格，便是为国家争人格！自由平等的国家不是一群奴才建造得起来的！”从“五四运动”到“六四民运”，每一次真正的爱国运动，都不是“奉旨爱国”的奴性举动，都是以自由民主为基调，以参与者的主体意志为动力。<br />
<br />
进一步使克尔西坚定的是他亲身体验的官方“教育”。在影片中我们看到，当时的匈牙利和苏联当局压制爱国民众，先是用宣传机器欺骗、威胁，继而使用催泪弹、高压水龙驱散示威者，最后出动坦克、警察和军队开枪镇压，甚至连手无寸铁的牧师也不能幸免，而这一切都是以“人民”的名义镇压“反革命暴乱”。对独裁者来说，“人民”只是工具和利用的招牌，当需要时，他们绑架民意，将自己的声音称之为“人民的愿望”，将自己的行为说成是“人民的意志”。当不需要时，他们将“人民的声音”污蔑成少数人的谣言或蛊惑，将自主行动的民众矮化成“不明真相”、受“煽动”“调唆”的“闲散人员”。总之，符合他们时就是“眼睛雪亮”，不符合他们时就是“受蒙蔽”，总之是诋毁民众的主体意志，而必须由他们来代表，民众不能自己选择，这是独裁者的一贯伎俩。<br />
<br />
当克尔西对朋友的死悲痛震惊之余，对血的代价是否值得产生质疑时，薇绮指出他的朋友“是为争取自由和独立而牺牲的。”只能靠境外“自由欧洲”电台了解真相的克尔西的祖父也鼓励他：“有时候反抗是必须的，尽管根本不该那样做。”和平主义者主张非暴力的基础是：对立双方都以尊重对方的生命为底线，谁破坏了这一原则就是承认对方也有“以其人之道还治其人之身”的合理性。当我们的权利被以剥夺生命相威胁时，证明我们作为人的权利和尊严就是生命的本质，必要时不得不选择拿起武器，用生命去争取和捍卫，尽管对双方来说是最坏的方式。如影片的一位演员所言：“人并不是想当英雄而选择牺牲，他们只是陷入困境别无选择，然后才成了英雄。”这里的“选择”就是遵从内心良知的自我实现。所以影片中作为新一代青年的演员坚定地表示：“把当年的情况搬到现在，我想我会毅然地投身革命，这就是历史留给我们的启示。”由此我想起1989年六四中个别战友的无耻蜕化，没过几年，当年的精神就消失了，他们转而忙着发财也就罢了，可是谈起六四民运竟全无起码的操守：“再有这事，让年轻人去堵枪眼吧！”并指责国内顶着官方迫害的压力、坚持表达异见的自由人士偏激，与今天的匈牙利青年相比，真令人汗颜！<br />
<br />
自由――是普世价值的最高标志，没有自由和权利何谈爱国？以“国家利益”代表自居的统治者，一旦剥夺了人民的自由和权利，就是剥夺了人民爱国的权利，就是与人民为敌，也就成了祖国的敌人，执政者就犯了“叛国罪”。因为国家机器的权力应通过民主程序被授权，在国家之上是民意、是人权，一个国家的利益应建立在自由民主基础上，而不是暴力之上，执政者的更替和维持通过民主选举，而不应是“枪杆子里面出政权”的强盗逻辑。<br />
<br />
经历了生死洗礼的克尔西一旦获得精神上的新生，他就义无反顾，此时奥运会对他来说已无意义，他不顾队友“往火坑里跳”的警告，冲破队友和秘密警察的阻拦，回到薇绮身边，一道拿起武器，并告诉她：“这并不光是为了你……爷爷说过：‘该还击时就得出手。’是到还击的时候了。”于是，如影片的演员们所说：“两个不可能交际的人就这样相遇了，并改变了彼此的命运，男孩教会女孩如何生活，女孩教会男孩心存信仰，并愿意为之付出行动。”懂得“一个人更重要还是信仰更重要。”“没有人知道会发生什么，他们只知道眼前的情形并不好，而他们必须做点什么。”后来当薇绮坦言父母入狱后一个秘密警察以能相救为名骗取过她的身体时，没有动摇克尔西对她的爱，共同的遭遇和理想使他们连成一体。<br />
<br />
革命不是常态，形势趋缓时，薇绮将克尔西劝回球队，当球队奔赴澳大利亚奥运会时，苏军和匈牙利反对改革的势力反扑回来，军队随着坦克、大炮布满街头，这边，薇绮他们浴血抵抗，另一边，同时进行的匈牙利与苏联的半决赛，成了奥运会上的另一个“战场”，在全场支持匈牙利的观众一边倒的呐喊中，尽管苏联队员大打出手，克尔西血染泳池……最后匈牙利队还是赢得了最终的胜利，展示了一个民族的不屈和尊严。面对升起的国旗，赢得冠军的匈牙利勇士们唱起庄严的国歌，此时，在狱中的薇绮被逼供出所谓“叛国者”的名单，她当着总理“菲尔大叔”的面，毅然写下：“斯大林、赫鲁晓夫、戈达、菲尔大叔”……薇绮和克尔西他们是民族自由独立的先驱，成为后代永恒的“孩子的荣誉”。<br />
<br />
据香港《开放》杂志2006年11期介绍：1956年10月23日至11月4日整整13天，20万苏军推翻了以改革派纳吉为总理的匈牙利政府（纳吉后被处以绞刑），有7800人丧生，12000人受伤，3万多人受迫害，几百名革命领导人被捕处决。20多万人逃亡西方，4700人流亡美国。<br />
<br />
遗憾的是，影片《孩子的荣誉》的国际视野还没有充分展开，不能不说，在这一悲剧中，中共罪恶的双手也沾上了匈牙利人民的鲜血：据吴冷西、徐铸成、师哲等人的回忆史料，出于投机实用的外交政策，毛泽东、刘少奇、周恩来和邓一改此前强烈反对苏联出兵波兰的立场，坚决督促和支持苏联武力干涉。因为波兰只是反对苏联的大国沙文主义，而匈牙利要废除一党专政，成立联合政府，要苏联撤军，主张中立化……刘宾雁曾说s“可以断言，中共领导集团和中共反动派造下的罪孽，远远超出今天已知的最大限度。它们是逃不过总清算和大审判的。审判席上还将有外国人在场。因为毛泽东一伙手上不仅染著中国人的血。1956年，苏军第一次进入布达佩斯后，曾一度撤出。是在毛泽东派出中共特使刘少奇赴莫斯科力促之下，苏联才再次出兵，对匈牙利人民起义血腥镇压的。”魏紫丹教授尚未出版的《从反右到文革》一书指出――那时中国存在着的内政“党天下”与外交“一边倒”所产生的政治弊端，正是爆发匈牙利事件的原因和条件。但他（毛泽东）决不会用消除政治弊端，来预防和克服匈牙利事件之爆发；而是为了保护这个“政治弊端”，坚决主张出兵镇压匈牙利发生的所谓“反革命暴乱”。实质上就是，进行一场国际的反右派斗争。这时，在他脑子里已打好要进行一场国内的反右派斗争的底稿。不是吗？我们已经清楚地看到，毛对二者都定性为镇压反革命，而且在克敌制胜上又都是同一思路，都是先“诱敌深入”，后“聚而歼之”。<br />
<br />
该书还披露，当时善良的匈牙利人并不了解中共，匈牙利革命爆发前，中共驻匈牙利大使郝德清曾向该国知识分子广泛宣传“双百”方针，还积极地和当时被压制的民主派联系，甚至得到了一份当时还没有恢复名誉的党内改革派领袖纳吉给中央的信的副本。纳吉在信中对中国提出的和平共处五项原则十分赞赏，说这五项原则也应该应用到社会主义国家阵营之间，这明显是在挑战苏联的大国大党主义。因此，匈牙利党内的民主派和该国知识分子一直把中国看得和苏联不一样，直到苏联出兵镇压时，很多人还说中国是站在他们一边的。然而，当匈牙利知识分子、学生和党内民主派把运动发展到要求改变共产党一党独裁的局面，退出苏联用来控制东欧卫星国的华沙条约组织时，中共大使馆的态度逐渐转变了。当时郝德清大使下令断绝和匈牙利人的来往，关闭使馆大门，每天把匈牙利的情况向北京报告。中共最终判断匈牙利的事件已经发展成反革命，在赫鲁晓夫动摇犹豫的时候强烈要求苏联出兵镇压，在这个决策中，大使馆的报告无疑起了很关键的作用。匈牙利事件之后，匈共领导人访问中国时，毛泽东告诉他们在匈牙利发生所谓“反革命暴乱”时，他每天都听取大使馆的报告，和其他领导人一起研究局势。从匈牙利事件中他吸取了教训，制定了在中国防止出现类似事件的方针。<br />
<br />
以上事实证明，中共是绞杀匈牙利革命的帮凶。我最初看到的国内关于匈牙利革命的书籍无一不将它称为“反革命暴乱”，从此后，中国历次运动中被迫害的知识分子许多人被冠以“裴多菲俱乐部”成员的罪名，“裴多菲俱乐部”就是匈牙利革命中著名的改革派知识分子团体。<br />
<br />
从史料可以看到，如同匈牙利人受到波兰人反抗的鼓舞，在1957年短暂的“大鸣大放”中，尽管有新闻封锁和歪曲，匈牙利革命等历史事件，对国人特别是知识分子的思想还是产生了深远的影响。所以每当提起苏东的历史和现实，有着相同命运的我们，都会感到亲切，他们争取民主自由的精神令我们钦佩，他们的经验仍是我们宝贵的资源。<br />
<br />
影片《孩子的荣誉》的制片人就是当年“匈牙利革命”的参与者和逃亡者，演职员们的祖辈或父辈都是亲历者，《孩子的荣誉》是他们向革命先驱们的致敬，今天的匈牙利，是他们与先辈共同奋斗成果，而作为经历了更多苦难和奋斗的民族，我们的“革命尚未成功”，我们以何种形象留给历史、告慰子孙？拿什么作为“孩子的荣誉”？ <br />
<br />
<br />
2008年10月23日于石家庄望云楼</div>

							
							
							
							
