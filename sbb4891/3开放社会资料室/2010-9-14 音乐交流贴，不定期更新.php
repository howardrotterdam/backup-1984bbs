音乐天堂合集<br />
自1991年创办的音乐天堂有声杂志，创刊来的12年当中不断以介绍欧美音乐文化为己任，在广大青少年以及大学校园里享有非常高的声誉。应新老读者的要求，特意将最早的一个阶段的15期集结成合集，作为中国乐迷及广大读者回顾收藏之用。 <br />
音乐天堂草创时期一切都是因陋就简，所以1-15期里以资讯为主，包含了少量的音乐评论，在更多情况下主要是对歌手的介绍，以及歌曲的背景资料。 <br />
全书为中国音乐爱好者们刻画了一条清晰的从无到有的道路。在那个资料全无的时代，人们是多么渴望音乐，多么希望听到更多更美妙的歌曲。那是一个值得怀念的时候，包含了如今已是黄金时代的人们的热爱、激情、梦想。 <br />
