<br />
原名Matt Shafer的Uncle Kracker，在数年前就是KID ROCK(摇滚小子)乐队的DJ，两者在1987年相识于一个DJ比赛会场，因为在音乐的喜好上有着许多相似之处，很快两人便合作玩起了音乐。<br />
