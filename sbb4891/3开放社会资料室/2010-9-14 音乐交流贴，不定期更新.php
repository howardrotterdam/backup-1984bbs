Tupac Shakur原名Lesane Parish Crooks，1971年6月16日出生在美国纽约的布鲁克林区，父母在他出生之前就已经离婚了，母亲带着Tupac Shakur和姐姐一起四处漂泊，家里一直非常的贫穷。Tupac Shakur生长在马里兰州的巴尔的摩市，并且在他十几岁的时候就进入了巴尔的摩的艺术学校学习表演艺术，在那里Tupac Shakur的说唱才华得到了非常大的发挥空间，他可以自编自唱说唱歌曲。但是在Tupac Shakur只有17岁的时候，还没有等到他从学校里毕业，他的家庭就被迫再次搬迁，他们最终在加利福尼亚州的奥克兰市落脚，Tupac Shakur也开始了在街头混日子的生活，在街头，Tupac Shakur开始和其他人一样为了生存而竞争，甚至包括乾一些违法的事情。几年之后，Tupac Shakur加入了奥克兰当地的一支乐团Digital Underground，Tupac Shakur在那里担任伴舞，后来他成为了他们的一名说唱歌手，在不久之后的一些列巡回演出中，Tupac Shakur渐渐形成了自己的风格，并且成为了Digital Underground的一个主要成员。<br />
<br />
1991年，Tupac Shakur作为Digital Underground乐队的成员，参与了乐队专辑《This Is an EP Release》和《Sons of the P》的录制工作，不久之后，在1991年11月份的时候，Tupac Shakur发行了自己在Interscope唱片公司的第一张个人专辑《2Pacalypse Now》，这张专辑在发行之后取得了相当不错的成绩，但是这张粗话连篇的专辑却受到了美国上至白宫政要，下至平民百姓一致的抨击和谴责。在此之后，Tupac Shakur借助着两部电影，《Juice》和《Poetic Justice》中的表演而渐渐的提高了自己的知名度，并且在1993年发行了第二张专辑《Strictly 4 My N.I.G.G.A.Z.》，这张专辑迅速进入了说唱排行榜的高位，并且不久之后销量就突破了100万张，其中的单曲也徘徊在说唱单曲榜的前十名附近。在1993年末，Tupac Shakur还出演了电影《Above the Rim》。<br />
<br />
虽然Tupac Shakur通过唱片和一些很成功的电影逐步的成为了一名说唱明星，但是，这个过程并不像文章叙述这么顺利，Tupac Shakur一直就麻烦不断，他频频的跟警察和监狱打交道，其中最严重的就是在1992年的时候，Tupac Shakur曾经因为与人争斗，结果在打斗过程中一颗流弹击中一名6岁小孩，小孩当场死亡，Tupac Shakur虽然被捕入狱但是最终还是侥幸逃脱了法律的制裁，其中一个重要原因就是他在成为说唱明星之前并没有犯罪前科，起码警察局的记录上是这么写的。尽管逃脱掉了，但是Tupac Shakur的麻烦一直没有间断过，1993到1994年，警察局和监狱成了Tupac Shakur第二个家。1993年夏天，在电影《Menace II Society》的拍摄过程中，Tupac Shakur殴打了导演Allen Hughes，结果在不久之后被判入狱服刑15天；1993年秋天，Tupac Shakur向两名下班的警官开枪，结果最后又逃脱了惩罚；1993年冬天，Tupac Shakur和两名随从人员涉嫌性虐待一名女歌迷结果被捕，并且在1994年初被判有罪。但是在他还未接受服刑的时候，1994年，Tupac Shakur在纽约的录音室遭到了两名歹徒的枪击，并且受伤。1995年2月7日，Tupac Shakur最终被判入狱服刑四年零六个月，但是尽管身处牢狱，Tupac Shakur并没有放弃自己的音乐创作。<br />
<br />
在Tupac Shakur入狱以后不久，1995年3月，他在服刑期间发行了第三张个人专辑《Me Against the World》，这张专辑奇迹般的成为了Billboard排行榜的冠军，由此也奠定了他在当时说唱音乐界第一人的地位，同时Tupac Shakur也是第一个在监狱中发行专辑并且成为排行榜冠军的人，这张专辑的总销量超过了600万张。但是Tupac Shakur在监狱里并不老实，他控告包括吹牛老爹Sean &quot;Puffy&quot; Combs，the Notorious B.I.G.以及Randy &quot;Stretch&quot; Walker等人在内的多名东海岸说唱歌手在1994年的时候在纽约制造了企图枪杀他的那起事件，据推测枪杀他的一个原因就是Tupac Shakur是一名出生自东海岸的人，但是他却在西海岸从事说唱音乐，Tupac Shakur扬言报仇，从而进一步激起了东西海岸说唱歌手之间尖锐的矛盾。在Tupac Shakur服刑八个月以后，Death Row Records唱片公司的老板Suge Knight为他支付了140万美元的保释金之后，Tupac Shakur重新获得了自由并且成为了Death Row Records唱片公司的一员，Suge Knight在任何事情上都非常支持Tupac Shakur，包括和东海岸说唱歌手们的争斗。<br />
<br />
1995年末，Tupac Shakur开始录制他在Death Row Records唱片公司的第一张专辑，但是在11月底的时候，Randy &quot;Stretch&quot; Walker在纽约的皇后区遭到枪杀身亡，这激化了东西海岸说唱歌手的帮匪争斗，也为Tupac Shakur悲剧性的人生结局埋下了祸根。1996年年初的时候，Tupac Shakur发行了双CD的专辑《All Eyez on Me》，这也是说唱音乐历史上第一张双CD专辑。这张专辑再次成为了市场上最受欢迎的说唱专辑，销量不久就超过了600万张，其中他和Dr. Dre合作的单曲《California Love》名噪一时，此时Tupac Shakur已经是当时说唱音乐界最成功的超级明星。不仅在说唱音乐界成就突出，而且在电影事业中也是非常顺利，到1996年夏天的时候为止，Tupac Shakur又参与了电影《Gridlock'd》，《Bullet》和《Gang Related》的演出。在专辑发行之后，Tupac Shakur又录制了非常大量的单曲，但是随着他和Dr. Dre关系的破裂以及东西海岸的矛盾越来越激化，导致Death Row Records唱片公司的老板Suge Knight被控涉嫌违法行为而入狱，这些单曲并没有机会发布。<br />
<br />
1996年9月13日，内华达州的拉斯维加斯，Tupac Shakur的生命定格在了这里。尽管此前Tupac Shakur已经厌倦了说唱明星的工作，已经着手退出乐坛，但是在他看完了Mike Tyson对Bruce Seldon的拳击赛之后，刚刚走出米高梅中心，Tupac Shakur遭到了一名黑人枪手的袭击，身中四枪，顿时倒地不省人事，六天以后的9月13日，Tupac Shakur被宣布死亡。关于Tupac Shakur被暗杀的原因有多种说法，可以肯定的是由于东西海岸说唱音乐的争斗，也有说法是此前被Tupac Shakur指控的the Notorious B.I.G.的妻子，R&amp;B女歌手Faith Evans和Tupac Shakur通奸，枪杀是the Notorious B.I.G.恼羞成怒的报复行为。但是在此之后，美国东西海岸说唱歌手的黑吃黑依然在继续，在Tupac Shakur死后不到六个月，1997年3月9日，the Notorious B.I.G.也被枪杀身亡。<br />
<br />
在Tupac Shakur死后，美国有大量的歌迷参加了他的悼念活动，而整个美国娱乐界也因为这样一位当时最出色的说唱天才的被杀而表示哀悼。尽管Tupac Shakur生前恶名在外，但是他的音乐依然受到了非常大的追捧，Tupac Shakur生前没有发行的大量单曲在他死后纷纷推出，1996年，Death Row唱片公司发行了专辑《Don Killuminati: The 7 Day Theory》以及在1998年发行了精选《Greatest Hits》，专辑《Don Killuminati: The 7 Day Theory》成为了他身后专辑中的首个排行榜冠军专辑；1997年，Jive唱片公司发行了专辑《R U Still Down? (Remember Me)》；1999年，Interscope唱片公司发行了专辑《Still I Rise》，2000年又发行了专辑《The Rose That Grew from Concrete, Vol. 1》，2001和2002年又先后发行了专辑《Until the End of Time》和《Better Dayz》，这些专辑都不同程度的取得了比较好的销售成绩，《Until the End of Time》更是再次名列排行榜冠军。2003年，Tupac Shakur的自传电影《Resurrection》的同期专辑《Tupac: Resurrection》正式发行，发行后首周即登上排行榜亚军，首周销量超过43万张。2004年，Tupac Shakur死后发行的第九张唱片《2Pac Live》发行后也曾经达到了排行榜第54名。<br />
<br />
2004年12月14日，Tupac Shakur去世后的第十张唱片，也是其中的第七张新专辑《Loyal to the Game》正式发行，并且再次取得了成功。自从1996年Tupac Shakur被枪杀之后，他所发行的专辑在美国地区销量已经达到了1800万张，整个音乐生涯的唱片总销量也达到了2440万张以上(转自gilbert.wang )<br />
