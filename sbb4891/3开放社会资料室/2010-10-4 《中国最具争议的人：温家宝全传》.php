虽然此说法并未得到曾与温家宝一起工作过的同事的证实，但了解温家宝这段经历的人在接受记者采访时，则透露温家宝初到地质力学队时，的确曾在农村劳动锻炼了一段时间。不过，此次锻炼并不是因为他同情“反革命”而被“发配”到农村的，而是当时的相关规定--地质队的新人首先要接受劳动锻炼。<br />
<br />
就这样，1968年8月，温家宝到酒泉地质力学队报到后不久，便同几位新同事一起被派到酒泉市肃州区丰乐乡二坝村接受劳动锻炼。<br />
<br />
现年70岁的杨虎学是二坝村农民，60 年代末曾担任过二坝村大队的革委会副主任。他对当年温家宝劳动锻炼的情景记忆犹新。<br />
<br />
杨虎学说，记得那是1968年9月。温家宝和另外四个男同志及两个女同志来到二坝大队劳动锻炼。那时候运输工具少，运肥料主要依靠大车。杨虎学经常赶皮车，温家宝和其它几名地调队的年轻大学生就给皮车装肥料。对温家宝，杨虎学最突出的印象是：话不多，但干活很踏实。后来丰乐公社在台子沟修水渠，社员们给渠装背石头，温家宝和社员一样背，并不比当地劳动力背的少。<br />
<br />
与温家宝再次见面是1970年7月，那时候，杨虎学在镜铁山二把哈垃领着社员修备战路。有一天，温家宝和地调队一位叫小米的同事拉着骆驼，路过二把哈垃，天快黑了，走的十分疲倦。杨虎学走出帐篷遇见温家宝和小米时，见两人嘴唇干裂。在这海拔3000多米的地方，见到丰乐公社认识的熟人，温家宝显得很高兴。第二天早晨，天刚蒙蒙亮，惦记着工作的温家宝和小米就出发了。<br />
<br />
谈起这件事，杨虎学显得很激动，温家宝以及地调队同事们的吃苦精神令他今生难忘。<br />
<br />
陈志丑是酒泉市肃州区丰乐乡三坝村五组农民，70年代曾在酒泉火车站当过几年装卸工。这期间，他有幸认识了温家宝。对温家宝的印象，陈志丑概括了四个字：朴素，随和。陈志丑回忆说，当时他在火车站行李房干活。温家宝经常到火车站行李房，主要领取从外地调运来的地质、水文器材，每一次去温家宝都和和气气，跟装卸工人聊上几句，器材一到就亲自动手往车上装，时间久了也跟工人打成一片，与大家开玩笑、劳动。<br />
<br />
在酒泉市丰乐乡二坝村三组的杨恒学家中，温家宝曾住过一段时间。时间虽然短暂，但温家宝的好学，给杨恒学留下了很深的印象。<br />
<br />
杨恒学回忆说，温家宝是在 1968年9月劳动锻炼时住到他家的。他们一起有好几个人，女同志跟三队的两位老大娘住在杨恒学家的伙房里，温家宝和另外几位男同志住在杨恒学家的北屋里。<br />
<br />
刚来时正赶上生产队打胡麻，温家宝虽然不是太会，但还是很勤快地同社员们一起摊场，并学着扬场。打完胡麻，社员们都上祁连山一个叫台子沟的地方修水库，温家宝也跟着去了，同大伙儿一起挖渠沟，背石头。<br />
<br />
杨恒学说，当时劳动强度特别大，温家宝他们在社员家里吃派饭。吃的最多的是苞谷面和小米饭。尽管生活条件艰苦，可温家宝一闲下来就裹着老羊皮袄看书，看完一本又一本，温家宝的生活也很有规律，每天总是早早起床，把院落打扫干净，再听收音机了解外面的世界。<br />
<br />
如今，温家宝当年住过的房屋都还在，虽然历经几十年，房屋已破旧不堪，但杨恒学一只没拆，他说：“看到这间房屋，当年那个好学的‘温技术员’像又站在了我面前了。”（《中国最具争议的人：温家宝全传》由明镜出版社出版）</div>

							
							
							
							
