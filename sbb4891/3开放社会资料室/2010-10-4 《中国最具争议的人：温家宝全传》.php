													<h2>温家宝名如其人，跟队友亲如兄弟</h2>
						
						
																											<div id="postmessage_854952" class="t_msgfont">温家宝名如其人，跟队友亲如兄弟<br />
<br />
发布： 2010-10-04 07:45 | 发布者： 推介 |<br />
<br />
《中国最具争议的人：温家宝全传》作者刘钺声<br />
<br />
在酒泉市肃州区丰乐乡二坝村接受近一年的劳动锻炼，到了1969年夏天，温家宝便和几位新同事一起返回了酒泉地质力学队。<br />
<br />
回到地质队，温家宝就当上了技术员，工作主要是从事祁连山地区区域地质调查，也就是填出不同比例尺的地质图以供找矿使用。这是最基础的地质工作，也是最艰苦的一项工作。<br />
<br />
温家宝从地质技术员干起，常年奔波在野外，用当年一起工作过的老同志的话讲，就是一年四季穿皮袄，没见过夏天。有关地质部门至今仍在使用的祁连山1：20万区调图就是在当年还是技术员的温家宝的参与下绘制完成的。<br />
<br />
王志恒是当时地质调查队的工程师，由于是地质方面的专家，退休后又被返聘回甘肃第四勘查院（简称“四勘院”，即当年的力学队、地调队），提起当年从事野外地质调查的艰苦和温家宝参与绘制的祁连山1：20万区调图，王志恒比划着告诉记者，当时队里的区调工作主要集中在祁连山区，很多地方根本没有路，最好的交通工具就是牦牛和马。夏天进山，雪水融化，一天过三四次河是常有的事。那幅温家宝参与绘制的区调图是难度最大的一幅，就拿祁连山主峰来说，那里人迹罕至，几乎全部是冰川，测绘的艰难可想而知。<br />
<br />
陈延京，山东省济南市历城区遥墙镇大陈家村人，1946年生，比温家宝小四岁。1964年底参军，在济南军区兖州某炮兵部队干气象兵。凭着“根红苗正”和突出表现，1971年3月，复员转业的他和另外九名战友被选送到到甘肃地质局酒泉区域地质调查队，成了一名地质队员。且在那里一呆就是十年。 1979年底调回山东，仍从事地质工作，直到2002年正式退休。<br />
<br />
当时的酒泉区调队400多人，大队下设分队，分队下设小组。分队30余人，小组五六个人。分组时，陈延京正好被分到了温家宝所在的第五分队地质普查组。当时他在这里任地质技术员，已经三年了。接下来的日子，朝夕相处、年复一年。虽然小组成员每年都有调整，但他俩作为党支部成员，一直固定不动，工作中相互配合，生活中亲如兄弟。<br />
<br />
谈起第一印象，陈延京想了想说，那时的温家宝身子比较单薄，中等个头，圆脸，皮肤较黑。穿一身蓝色的工作服，虽然不是很新，但很整洁。话不多，但很谦虚、稳重，透着热情。之所以印象这么深，一是听说他是从北京地质学院主动要求来这里工作的研究生，当时可是个了不起的高学历，很崇拜；二是他的名字有特点，容易使人想起“温暖”和“饱饭”，很舒服，因此一下子就记住了。<br />
<br />
区调队的任务是搞区域地质调查，即填出不同比例尺的地质图供找矿等用。这是最基础的地质工作，也是艰苦中的艰苦。工作范围涉及甘肃、新疆、西藏的部分地区，工区内有雪山、草原也有沙漠。每年3月份出发，先骑着牦牛到白雪皑皑的山中；10月份后，天气变冷，就改骑骆驼，到沙漠里作业。通常12月底天寒地冻时才返回基地酒泉。工作中一般是以小组为单位集体行动。<br />
<br />
在山上工作时，一般要爬到海拔四千多米的山，那儿的积雪常年不化，大家穿着皮裤皮袄出工，经常会冻得发抖。每天十几公里跑下来，脚都肿得厉害。回到宿营地，大伙便忙着取冰化水，生火取暖、做饭。简单的吃过后，一般人钻进帐篷躺倒行军床上倒头就睡。温家宝却常常睡的最晚，因为他负责技术，每天的资料要整理，后来他担任了小组负责人，还要准备明天的工作。几乎天天都要催促他好几遍，他才躺下。有时为了夜里起来扫帐篷顶上的雪，他还要穿着衣服睡。当时他患有较严重的鼻炎，遇冷经常发作，因为高山缺氧，厉害时常常晕倒在山上。每次醒来后，稍微歇息一会儿，爬起来再接着跑。他常说，当天的任务必须当天完成，不能因为自己耽误队里的工作进度。<br />
<br />
在沙漠里搞勘查滋味也不好受。每次进去，都要用骆驼驮着大量的淡水，装在特制的半圆形大铝桶里，放在宿营地。为抓一个“早”字，每天天不亮，温家宝第一个起床，帮大家准备好简单饭食后，趁天凉快，抓紧去跑路线。常常是干着干着，太阳就火起来，脚下的沙子就烫起来，身上的汗就流出来、粘起来。尽管背着十几斤的水，那天也喝不到黑。汗水边流边蒸发，最后的结果是，回到驻地，两手往脸上一抹，盐渍就扑扑簌簌往下掉。条件再艰苦，小组的工作年年是按时甚至提前完成。（《中国最具争议的人：温家宝全传》由明镜出版社出版）</div>

							
							
							
							
