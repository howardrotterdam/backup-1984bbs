																											<div id="postmessage_854951" class="t_msgfont">温家宝主动要求去甘肃是为追求爱情？<br />
<br />
发布： 2010-10-03 08:23 | 发布者： 推介 |<br />
<br />
《中国最具争议的人：温家宝全传》作者刘钺声<br />
<br />
说起温家宝初到地质力学队就和几位新同事到农村接受劳动锻炼，其中还包括几位女生，便让人想起在中国地质界有一段关于温家宝的传闻：温当初主动要求去甘肃，是为了追求同校的一个女生……<br />
<br />
旅居波士顿的“甲城老武”曾在2008年5月5日在网上发表一篇文章，说的是“听同事讲温家宝”。当年也曾在地质界“混了”多年的老武写道：<br />
<br />
老武虽然和温家宝同在地质部门工作过，遗憾的是从来没有亲眼见过他。他当地质矿产部副部长的时候，我在上研究生。当时，我经常在《地质报》上看见这位排在后面的副部长的名字，总觉得这个名字有点土气，但也好玩。我硕士学位完成后就留在北京的研究机构，这时家宝已高升为中央办公厅主任，官比部长朱训大得多。<br />
<br />
温家宝在读研的时候赶上了文化大革命，未毕业便去了位于甘肃酒泉的甘肃地质局地质力学大队。有的同事说温家宝去甘肃酒泉是因为当时在地院追求一个女生，人家没有答应。那个女生后来去了兰州，温家宝便跟着去了兰州。到了兰州人家还是没有跟他，温家宝就干脆去了酒泉。这个故事是真是假，本人没有考证。<br />
<br />
温家宝到了酒泉，桃花运就来了。地质队就有两个不错的姑娘追逐他，一个是江苏的，另一个就是现任夫人张培莉。温家宝的自身条件也非常好，其一是共产党员，其二是研究生学历，其三就是人长得也不错。温家宝原本对那个江苏姑娘情有独钟，但经不起张的糖衣炮弹，几个回合下来家宝就被张给捕获了。<br />
<br />
张培莉在地质队有个外号叫“一阵风”，意思是做什么事都是风风火火，而且非常干净、利索。每次温家宝从野外回来，人还没进屋，张已经把洗脚水给他打好了。吃饭的时候，温家宝还没来得及动身去食堂，张培莉已经替他打好了饭。温家宝有一个毛病就是嘴巴把不住风，经常把队里的一些内部消息讲给老婆，老婆又把它传出去。<br />
<br />
不管老武讲的这个“温家宝因追地院女生而主动要求去甘肃工作”的“故事”是真是假，但有一点是可以肯定的，他的确在到酒泉地质力学队工作没多久，便“无心插柳柳成荫”，“抱得美人归”了。<br />
<br />
从温家宝当时的政治表现、性格和为人来看，说他是为了追求女生而主动要求去甘肃工作，似乎不符合其温文尔雅的形象，可爱情的动力是难以估量的。据说，温家宝与兰州大学地质地理系毕业的张蓓莉认识没多久，两人便喜结连理，比翼双飞了。（《中国最具争议的人：温家宝全传》由明镜出版社出版）</div>

							
							
							
							
