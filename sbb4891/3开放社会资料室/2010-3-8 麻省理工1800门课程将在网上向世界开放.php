													<h2>麻省理工1800门课程将在网上向世界开放</h2>
						
						
																											<div id="postmessage_507979" class="t_msgfont">原帖作者： snorkeler<br />
<br />
今年年底之前，世界知名学府、美国麻省理工学院(MIT)全部约1800门课程将向全世界开放。学习者足不出户就可在互联网上学习，并且一切免费。网址:<a href="http://www.core.org.cn/OcwWeb/index.htm" target="_blank">http://www.core.org.cn/OcwWeb/index.htm</a>。<br />
目前，旨在推广应用MIT开放课程的镜像网站已在我国建立，该网站网址为:<a href="http://www.core.org.cn" target="_blank">http://www.core.org.cn</a>，高等学校联合机构“中国开放式教育资源共享”负责该网站的维护。中国学生可以直接登录该网站，免费获取MIT开放课程信息。<br />
“开放课程”使教育发挥最大潜能<br />
美国《基督教科学箴言报》报道说，位于马萨诸塞州的麻省理工学院2002年开设“开放课程计划”(OCW)网站，现已公布1550门课程资料，今年年底之前其余课程材料也将在网站上公布。<br />
至今全世界已有大约120所院校参与到这项计划中。除麻省理工外，马里兰州约翰斯・霍普金斯大学、密歇根州大学、印第安纳州圣母大学、犹他州大学等都是计划合作院校。<br />
作为一项“智力慈善事业”，“开放课程计划”提供教学大纲、视频和音频课程、笔记、家庭作业等。参与计划的院校并不鼓励学生注册，只是激发学生的学习热情。<br />
麻省理工“开放课程计划”执行主任安妮・马古利斯说:“我们认为，当知识得到公开和自由分享时，教育也能得到最大限度发展。麻省理工利用因特网的力量，把这里编制的所有教育材料分发出去。”<br />
远程教育在许多国家方兴未艾。与许多院校相比，麻省理工与合作院校提供的材料数量简直惊人。麻省理工助理教务长玛丽・李说，计划合作院校马萨诸塞州塔夫茨大学提供29门课程，其中每门课内容相当于一本教科书。<br />
除课程内容外，麻省理工努力展示其教学方法。许多院校通常采取传统的教学方法，即首先教授理论，然后让学生练习所学知识。麻省理工采取“实践、理论、实践”的教学方法，其目的是让学生投入学习，在深入钻研理论之前感受学习乐趣。<br />
全世界学生对此“一网情深”<br />
麻省理工和合作院校网站还把教学材料翻译成英文以外的其他语言。据马古利斯介绍，每月平均约140万人次浏览网页，其中包括伊拉克、苏丹达尔富尔地区，甚至南极洲居民。<br />
随着浏览人数增加，网站收到越来越多学生的积极反馈。摩洛哥物理学教授尤尼斯・阿塔乌提上网时无意中发现“开放课程计划”网站，从此“一网情深”。他说:“我认为，世界上没有哪个地方的大学比这家网站更慷慨。这是全世界的人们第一次接触到高质量课程。”<br />
来自中国的学生陈志英说:“因为没有钱，许多聪明勤奋的学生没有机会学习最新知识和了解宇宙。但现在，因为‘开放课程计划’，知识传播到越来越多的人，并将惠及整个人类。”来自法国巴黎的学生莱利・帕斯科说:“麻省理工‘开放课程计划’是一项慷慨和有远见的计划，比1000次伊拉克战争改变世界的作用还大。”<br />
课程仍面临资金技术等难题<br />
“开放课程计划”发展过程中面临资金、技术等难题。<br />
在所有课件中，全视频资料最受学生欢迎。然而，在1000多门课程中，眼下只有26门课程提供视频材料，共计约1000小时。增加视频材料面临资金短缺窘境。“我们希望制作更多视频材料，因为视频材料相当受人欢迎，使用者也喜欢，”麻省理工“开放课程计划”执行主任马古利斯说，“但视频材料相当昂贵。”马古利斯说，“开放课程计划”依靠社会团体、个人慷慨解囊和麻省理工自筹资金。<br />
技术层面，“开放课程计划”网站需要进一步改进。“我们需要非常快的宽带网和通讯工具，”计划合作院校斯坦福大学继续教育项目主管说。(综合报道史先振盖明)<br />
