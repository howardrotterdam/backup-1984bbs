<a href="http://news.sina.com.cn/pl/2009-04-23/074717669911.shtml" target="_blank">http://news.sina.com.cn/pl/2009-04-23/074717669911.shtml</a><br />
<br />
　　作者：五岳散人<br />
<br />
　　这种所谓的“表面无伤”之手法，往往会造成更大的人身伤害。民生艰难，如果真要用这种类似于“武林秘籍”与“一招制敌”的东西，对待生活已经足够艰辛的小商小贩，其心何忍啊。<br />
<br />
　　最近两天流传的《城管执法操作实务》被证明确有其事。其中争议最大的那段“注意要使相对人的脸上不见血，身上不见伤，周围不见人”也不是凭空而来，而确实是作为指导城管行动的准则之一，白纸黑字。北京市城管部门一位不愿意透露姓名的人士惊呼：“谁把它弄到网上去了？这些应该是内部的东西怎么能拿到外面去说？”而编撰人之一的高锋也说：“这是断章取义。实际的意思不是这样。”(昨日《南方都市报》)<br />
<br />
　　网民惊呼：敢情要教城管练就武术中的“化骨绵掌”？<br />
<br />
　　笔者读书不成而学剑，学剑不成再读书，虽然两无所成，但两者多少都是知道一些的。引发网友热议的这句话从文字角度来说，是否算是“断章取义”呢？高锋先生说：“当初没有将‘禁止把人脸打出血，伤害到身体’之类的话写入教材，是考虑到这样的话过于直白和罗嗦，所以才用了‘脸上不见血，身上不见伤’的简洁提法”，“执法人员绝不能向执法对象大打出手，不能把人打得满脸花甚至伤害到违法者的身体，执法不能过度”。原来这是为了控制城管所使用的暴力程度，而并非像网友所说的“以暴制暴、掩饰暴力痕迹”。<br />
<br />
　　但我想，如果真像高锋先生的意思，那“脸上不见血，身上不见伤”这10个字其实完全不用这么表述，最简单的表述应该是“不得过度防卫”，而指出对别人的伤害不能在外面表现出来，恐怕既不是最简洁、也不是最合适的做法。而且后面还有五个字叫做“周围不见人”。可是，城管工作的场所都是在闹市区，在荒郊野外对谁执法呢？而在闹市区还要用“周围不见人”的说法来实施所谓的“合适的暴力”，其用心恐怕就真是不堪问了。<br />
<br />
　　这个“周围不见人”是最大的关键所在，有此一语，其他的辩解都是无效的。城管作为一个执法单位，即使在现阶段其法律地位存在疑问的状态下，依然算是代表着国家的权威。那么试问，什么样的执法行为是在“周围不见人”的情况下，用表面上看不到伤情的方式进行的呢？<br />
<br />
　　我所深知的是，这种所谓的“表面无伤”之手法，往往会造成更大的人身伤害。高锋先生所说的“言外之意”其实在现实格斗中是不存在的，任何人在这种情况下都不可能外表无伤。这种说法是混淆视听，为这本书中所体现出的杀气找一个借口而已。民生艰难，如果真要用这种类似于“武林秘籍”与“一招制敌”的东西，对待生活已经足够艰辛的小商小贩，其心何忍啊。<br />
<br />
　　但笔者也注意到，这个内部资料实际的曝光者是城管机构的内部人员。如果说在这份材料让人震惊之余还有某种希望的话，这位城管工作人员曝光此材料所体现出的良心，恐怕是唯一的亮点了。仅此一份良心的闪光，就足够让人看到，即使在经过了这种培训的地方，依然有很多人具有着最起码的良知，这也是我们社会能够继续往好的方向发展的一个动力。<br />
<br />
　　昨日，北京市城管执法局给予回应，承认确有此书存在，但“未在城管一线执法队员中进行统一培训”。如果真是这样，为京城的那些小商小贩们庆幸。但是，一个有违“以人为本”理念的的文本冠冕堂皇地写进正式出版的“培训教材”里，总是有引起人们警惕的东西。<br />
