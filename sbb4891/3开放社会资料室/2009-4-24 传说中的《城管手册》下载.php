<br />
　　这样的执法经验，自然激起了舆论的愤怒。有网友戏称这是流氓培训手册，是城管执法的武林秘笈和葵花宝典，认为此书“重新定义了赤裸裸的暴力执法”――确实如此，久遭舆论诟病和狠批的暴力执法，竟被人在系统化、理论化、技巧化、隐蔽化中包装成“操作实务”，并公开出版在系统内津津乐道地当经验交流。<br />
<br />
