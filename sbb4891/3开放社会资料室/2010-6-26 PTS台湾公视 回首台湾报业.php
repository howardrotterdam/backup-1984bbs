													<h2>PTS台湾公视 回首台湾报业</h2>
						
						
																											<div id="postmessage_705226" class="t_msgfont"><a href="http://bbs.verycd.com/topics/394624/" target="_blank">http://bbs.verycd.com/topics/394624/</a><br />
别名：PTS台湾公视,9集全<br />
资源类型：TVRip<br />
发行时间：2006年<br />
地区：台湾<br />
语言：普通话<br />
简介：<br />
为什么作为⒚擅裰堑谋ㄒ担会被视为今天社会的乱源？追本溯源，要知道台湾报业的历史，才能分辨所有的新闻。公视「回首台湾报业」纪录片中，从经济、与读者的关S、政治、社会、副刊、影剧、报纸版面、记者角色等主题，省思台湾报业的发展及未来。<br />
「回首台湾报业」最早由前任董事长吴丰山先生建议筹备，a於近年来由媒体所引发的争议特别频繁，而报业作为国内媒体中历史最悠久、影响力也最大的一环，在当下u作历史回顾检讨，特别有意义。在长达一年的u作过程中，u作小组除了跑遍位於全省的各家报社，实际记录报纸生产、运送和贩售的过程之外，也远赴香港、北京及美国，和一些资深报人进行当面访谈，可以说华人跟报人跑多远、中文报纸的影响力就有多远、「回首台湾报业」的触角就有多长。从名重一时、桃李满天下的学术菁英，到一言九鼎的业界大老、犀利敏锐的新生代报人，畅所欲言，让整个节目精彩丰富，充满感性和批判性。<br />
<br />
此外，片中也借重动画的影像包装，一方面凸显了报业既有的插画传统，也打破观对於所谓纪录片既有的观念，流动的影像、明快的节奏、以及动画里饱含的寓意和批评，打破纪录片沈闷枯燥的既定印象。导演王传舷表示跟新闻有关的事件，在u作期间，也源源不断发生，例如台湾日报的财务危机和劳资纠纷、中晚的结束营业等等，这说明历史是活生生在进行，「回首台湾报业」也不只是一个「回首」。<br />
<br />
第一集 政治阴影下的报业 <br />
话说开天V地，台湾在十九世纪有了报业，但是在日治时期它是童养媳、国府戒严时代又有家暴，一路鼻青脸肿、身不由己，让我们辛酸地从头说起。<br />
<br />
第二集 声喧 各拥其主 <br />
报业解禁后，呼吸到自由空气的报业，打著监督的口号，成了恶婆婆？政党恶斗、 报业各拥其主，应该旁观监督的人，演得比戏子还卖力，这一集将检讨新闻的失序。<br />
<br />
第三集 经世济国将本求利 <br />
说什么文人办报、正派办报，报业难道可以不食人间烟火？经济是一b看不见的手，温柔或粗暴地伸进报业每个角落，这一集将揭露报业最赤裸裸的本色。<br />
<br />
第四集 扭曲社会的新闻 <br />
可以夸大报导杀人放火、不可以不反共爱国，漫长的戒严时代，培养出台湾报业独特的社会新闻传统。社会版是报业的摇钱树，愈不景气、愈不堪入目，本集将探讨社会版成形的来时路。<br />
<br />
第五集 群星在副刊闪耀 <br />
琼瑶、古龙、高阳、三毛，数不清严肃的、通俗的作家在副刊上以_纷的想像力连载、陪伴我们渡过那段漫长惨白的戒严时代，副刊是台湾报业最傲人的奇，这一集将分析奇的源起。<br />
<br />
第六集 百花在影剧版开放 <br />
为什么影剧版是报屁股的屁股？虽然没有得到应有重视，影剧版的存在却也历史悠久，随著台湾娱乐工业不同领域的兴衰，报上的花花草草也随之荣枯，本集将追述影剧版的故事。<br />
<br />
第七集 方圆之间无穷版面 <br />
一块版面上放两万个字、跟只放八千个字有什么不同？有人说戒严时代三大张报纸的限制，造成台湾近视人口的普及，版面除了影响健康、其实还提供美学、灵魂的营养，这一集给观看科技和美学的发展。<br />
<br />
第八集 顺流逆流新闻人 <br />
警察怕记者、记者怕流氓？新闻工作是一个在享有特权与荣宠的同时，无时不刻天人交战、动m得咎的行业。这一集让资深新闻人告诉观，为什么记者在外一条龙、回到报社却成了一条虫。<br />
<br />
第九集 打开识野读新闻 <br />
以前的报纸可以教育民，网路时代的村民，反过来用零成本的部落格教训报业， 这是个什么世界？反被动为主动，这一集将告诉观为什么匡正媒体乱象、人人有责。<br />
