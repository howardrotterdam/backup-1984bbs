以下转自豆瓣影评：<br />
　　终于看到了传说中的《地下通道》，这是《省港旗兵》系列的第四集，最吸引我的倒不是“省港旗兵”的名头，而是敏感的题材和“麦当雄制作有限公司”出品。在《颐和园》之前大概只有《地下通道》采用这种时代背景吧，由于我对之了解非常少，所以好奇心又作祟了。 <br />
　　 <br />
　　《地》主要讲省港旗兵们将三个学生代表（三个代表？）从大陆偷运到香港的故事，有着香港电影标志性的剧情娱乐化和戏剧化，虽然影片和陆泗结合很紧密，但却没有对苦难记忆的缅怀，也没有进行深一层的揭露，如果脱离了那时代背景便与一般枪战片无异。《地》片很多东西都很表面化，比如人物设置，比如他们的举止行为。主要反派是政治保卫局的袁同志，影片中他基本上就是一个穷凶极恶的法西斯主义分子，杀人不眨眼，还带着一帮“港穗旅游团”跟香港飞虎队火拼，这样做很容易让观众有直接的代入感，融入剧情，与主角一起出生入死，但却没有了任何思考的余地，不会去想是什么造成这种人的出现允许这种人横行，也很容易让人忽略其真实性，这里是指那特定历史阶段下，各种事件，各类人等的真实性，我相信当时比袁同志更狠更狼的大有人在，但是这种夸张的处理方法容易让人忽略其整体的构成存在。 <br />
