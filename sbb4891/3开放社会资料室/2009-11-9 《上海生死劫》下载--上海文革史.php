　　 郑念对文革的个人叙述《上海生与死》出版于1987年。出版后即成为英语世界畅销书。好评如潮，连后来的诺贝尔奖获得者约翰&quot;库切都忍不住在《纽约时报》上写书评，推荐这本书。使作家约翰&quot;库切感到惊异的是本书主人公的勇气，他评价为，“在人的水平上，她的回忆录的最伟大的可贵之处，在于她对自己抵抗心理和身体的压力记录。这种压力可能使大多数人崩溃，而她的抵抗加续到一个重要无比的时刻，在那刻，她手带拷镣，牙龈溃烂，体内不停地大出血，被疑是子宫癌，在这个时刻，她被通知说，由于无产阶级的伟大力量，她被释放了，可以回去重过以往的日子了。她拒绝了自由：她宁愿待在狱里，她说，直到这个政权向她道歉，并在北京和上海的报纸上公开。她拒绝屈服，直到她被强扭着扔到街头上去。” 库切称赞这是一本“信息多，充满了勇气的，吸引人的书。” <br />
　　 <br />
　　　　郑念的身世在中国的背景下有些不同寻常。她是一个中产阶级职业女性。年青时代，她留学英国，并在那里遇到后来的丈夫。他们一起在一个激进的左翼的社会主义学院里学经济学。学成回国后，丈夫工作，她相夫教子，过着中产阶级平静的日子。她的丈夫曾为民国政府的外交部工作，1949年，选择留在中国大陆，期待能为建设新的中国出力。可惜，他并未得到政府特殊重用，幸运的是他在得到中央政府同意之后，受聘于英国壳牌石油公司驻中国办公室。壳牌公司是当时唯一留在红色中国的外国石油公司。红色中国需要外国的石油，需要郑念丈夫这样既受英国人信任，也没有那么多政治背景的人工作。在这种幸运之中，郑念一家的生活方式成为新中国危石之下仅存的几个完卵之一，继续维持过去的中产阶级生活。更幸运的是，1957年他因病去逝，免去了后来政治运动的灾难。可是，他的妻女将为他选择留在中国付出代价。 <br />
