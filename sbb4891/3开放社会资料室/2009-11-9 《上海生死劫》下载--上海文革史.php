　　　　1966年8月初，已经不再为壳派石油公司工作的郑念被揪到批斗会场，观看壳牌石油公司原总会计的批斗会。那时，壳牌公司已经停止与中国的贸易。中国在生产了自己的石油后，决定断绝与外国的石油生意。郑念已赋闲在家。一两个星期后，文革的冲击波到了她家。她被抄家，不久又被关进上海第一看管所，等待审判。在名不正言不顺的监狱里，她呆了六年，经受了无数的折磨，精神的，肉体的，但是她相信自己的无辜，发愤要与关押她的人斗争到底。这六年里，她与女儿毫无联系，1972年出狱后才得知女儿早已死了。据说是自杀，虽然郑念不相信她的女儿，那么热爱生活，与自己相依为命的女儿会在不知道母亲生死的状态之前自杀。郑念坚信女儿是被谋杀的。可是，女儿的死是文革初期混乱的结果，没有人愿意认真调查此事。1980年秋，在经过多年的周密计划之后，郑念终于得以离开中国，前往香港。壳牌公司又为她买了头等舱机票，她飞往加拿大，与妹妹会面，后来又移民美国，终于在首都华盛顿定居。 <br />
　　 <br />
　　　　中国的中产阶级在红色中国成立后就不断被批判和改造。除了那些享受共产政府特殊待遇的人，大部分的人经济地位都一落千丈。郑念是极为少有的以自己的能力，因为为外国公司工作而维持自己的生活方式的人之一。这种生活方式，不害人，不害己，没有任何可责备之处。但是，在一个疯狂地反对、害怕外国的红色中国内，任何与外国有联系的人都被看作是要破坏伟大红色国家的里通外国的间谍，更何况是为一个外国公司工作的职业妇女了。仅仅因为此，仅仅因为为外国公司工作过，郑念就被认定有罪，被投入监狱。导致家破人亡。一个国家的对外心态也反应其对内心态。高度不信任外国人，以为外国人都是敌人的这种心态，其实也是对内毫不信任的结果，也是对自己毫不信任的结果。这种时时刻刻担惊受怕地以为外国人要把自己毁灭，内部人想推翻自己的神经质状态，导致的是红色中国一次比一次疯狂的政治运动，结果是一次比一次更惨烈的对普通人生活的破坏，对人性的毁灭。 <br />
　　 <br />
　　　　看透了这一点，1980年9月20日，细雨蒙蒙之中，郑念离开了上海的码头，前往香港，告别了已经生活了65年的故乡。她多少次曾从这里上船，来往与上海和香港之间，此次，却是永别。是的，是她接受丈夫的请求，1949年带着唯一的孩子梅平从香港回到上海的，回来参与红色中国的建设。但是唯一的孩子梅平在23岁的花样年华死于文革。在离开之刻，她百感交集：内疚、伤心、决绝：“毕竟，老人先逝，年轻人继续生活是自然的法律啊，而不是反过来。我觉得悲伤。我在永远地离开我出生的国家。决绝如此最后，几乎使人碎裂。只有上帝才知道我是何等努力地忠实于我的祖国，但是我失败了，彻底失败了，却并不是由于我之错”。 <br />
　　 <br />
　　　　这是谁之错？郑念在书中明确地指出，这是一个政治团体的集体责任。书中对红色中国的政治、政府、监狱、街道委员会、人与人之间的关系的细节描述，为世界读者提供了一副生动的红色中国多层次生活的画面。只有在分析文革时，特别是她自己莫名其妙地被投入监狱的理由时，郑念实在是为那些制造她个人生活悲剧人找不出更多的理由，因为完全没有任何理由，郑念的分析则有些囿于她写作的时代背景之内。她把这场政治灾难看成是党内的派系斗争，对周恩来有种80年代的感伤怀念。今日读来，这个局限性显而易见。但是，郑念的这个局限，并没有妨碍她对红色中国做极为准确的观察和描述。她对共产制度的描述，至今仍极为有意义。细读此书，郑念的描述，可以把她置于中国最出色的观察家之一。 <br />
　　 <br />
　　　　本书最大的特点之一就是这种细节生动的描述和从一个自由知识分子角度对这个国家种种机制的分析。这本厚厚的书充满了生动准确的细节，把生活的生态展现给读者。同时，作为一个受过西方教育的知识分子，她也时时刻刻都在对这个异己的国家进行观察，分析。从文革的开始到她离开，虽然她被时代裹挟，冲击，击碎，但是她从来没有丧失过独立思考的能力。这种独立思考，是她的教育和思想背景和生活经验的结果。本质上，她是一个自由知识分子；三四十年代，她一定是一个左倾知识分子。在书中谈到她年青时学习的费边主义思想。费边主义的社会主义经济组织结构对这个以经济学为专业的人不可能没提供理想主义的蓝图。现实当然使她失望，使她对专制政权的本质有了新的认识。书开始的时候，她是这样介绍她对这个制度的认识，“自从共产时代开始，我极为认真地研读了马克思的著作和中国共产党领导人的文件。在我看来，社会主义在中国似乎是一个试验品，固定的发展过程还没有决定出来。这，就是为什么政府政策变个不停，好像钟摆从左到右摇摆不停。”“昨天对的，今天就错了，基层党领导人的言论只有效一段时间。” 对人民和政府之间的关系，郑念写到，“由于政府是中国人民命运的唯一的仲裁者，他们对政府代理人的每一个微小的举动都极为敏感，把这些举动解释成自己在权威眼里地位的标志。”对文化革命，她这样总结的到，“我认为文化革命(Cultural Revolution)可以更确切地命名为文化取消(Cultural Annihilation)。” 书中这些简短的似乎名言一样一针见血的句子比比皆是，反映出作者对这个制度的深思虑熟的所做出的结论。 <br />
　　 <br />
　　　　本书对文革的记录尤如史实，读起来好像在读一部文化革命史。郑念在记录史实的时候，既详细地记录了这些政治事件，也仔细地记录了这些事件怎样影响到她个人，并力图分析这种个人和政治事件之间的关系。这使这本书有种个人编年史的性质。这本书也以编年的方式写下来的，从文革开始，到抄家，到软禁，到被捕入狱，上海的一月革命和军管，党内斗争和她在监狱中的经历，直到释放回家，尼克松访华和她最终获得离开许可，发展的顺序与时间顺序同步，对那些对文革所知不多的人来说，这本书可以是一本文革史，特别是上海文革史，虽然是通过个人经历来展现的。由于本书具有比较广阔的视野，对文革中的种种政治风波、事件都有记载和分析，这本书也反应了一个中国自由知识分子对红色中国的个人思考。个人性与历史性的结合，使本书独树一帜，其独特的角度弥足珍贵。 <br />
　　 <br />
　　　　郑念个人的勇气浮现在书的字里行间之中。这种个人的勇气是以个人的抵抗的方式表达的。在本书初始，郑念试图保持自己的生活方式。这种努力本身，就是对强制性的一律化生活方式的抵抗。抄家的时候，红卫兵要砸那些她多年收集的瓷器，她以机智保护了大多数的收藏品，抵抗对中国文化品的彻底毁坏。在监狱里，她抵抗当局对她的种种精神迫害和身体折磨，她以极大的勇气，坚持不发疯，不毁掉自己，保持自己的头脑，保持自己的身体，甚至以背诵毛选来保持头脑的清晰。出狱的时候，她骨瘦如柴，体重不足80磅。在狱中她正值更年期，后来身体常常出血，当局以为她得了癌症，最后才放她出狱。一个人和一个强大无比的制度较量，需要的勇气也许不言自明。这种个人信念和道德坚持的勇气，对高压政治的抵抗，使她从没有背叛自己的良心。而她的弟弟，却在高压下，背叛了她。临出国前，她把自己多年收集珍藏但被抄家抄走了的瓷器捐献给了上海博物馆。对她来说，一生收集的物品都不值得再存有。 <br />
　　 <br />
　　　　郑念是一个聪明的、机智的、智慧的女性。书中的很多篇幅是她和监狱内的当局斗智的较量，在几乎不可能的情况下，在力量根本悬殊的狱中，她的机智聪明使她成为两者之间的精神胜利者。她的不妥协和对时局的分析，居然赢得了一些看守的尊敬。她这个六年没出过监狱门的人，仅凭文革时期的报纸，就敏感地分析出局势的变化，并依此作为自己抗争的论据。她对毛的著作，比审讯她的人还熟悉，她时时刻刻用毛的话来质询她的审讯者。使那些希图从她身上找到证据的人不得不放弃。她用自己的聪明才智是那些迫使她就范的人无可奈何。郑念是一个真正的智者。这种智慧，体现在作品中，是行文中的洒脱。中国文化不乏智慧，可是中国文学中慧性闪光的作品不多。 <br />
　　 <br />
　　　　而该书的英文纯熟优美，文字清新、洗练，更增加了阅读的吸引力。这不是一般人可以写的英文，是一个对英文文字有特殊造诣的人才能达到的洗练，生动和诗意。把这本书放在优秀的英语作品中，郑念的语言毫不逊色。 <br />
　　 <br />
　　　　《上海生与死》从一个人的角度描绘文革给她个人和这个国家带来的灾难。这种回忆录，我们不是太多，而是太少了。虽然本书也有其局限性，除了我上文提到的之外，书中那个名叫达德的年青人的故事，郑念把他写成一个跟探，似乎证据不那么充足。一朝被蛇咬，十年怕草绳。在一个兄弟姐妹，夫妻父母都可能互相出卖的文化里，郑念的警惕性也到了神经质的地步。可是我们能责备她么？ <br />
　　 <br />
　　　　有人曾说郑念是为讨好西方人而写这样控诉文革灾难的书的。这种论点完全站不住脚。在这些人看来，即使你受了苦，也不要控诉。持这种论点的人自己对中国遭受的苦难是瞎子聋子哑巴，也不想让别人看和说话，更不想让一个可以对世界说话的人说话。表面上是家丑不可外扬，实际上是还是无缘无故恐惧外部世界，仇恨外部世界，自我也毫无信任的老套。而且“讨好”两字，其实是反应了说这些话的人本身的潜意识的下等人“讨好”心态。郑念以个人的勇气，让世界知道文革的真象，这种书，越多越好。一个对自己历史有健忘症人，根本不了解自己谁是谁。一个对历史有健忘症的民族，也无法理解自己和想像自己的未来。中国的文化大革命，是二十世纪中国悲剧的集大成。我们对那段历史，不仅仅要弄清史料和很多事件的来龙去脉，还要对这场“革命”进行多角度的考察和分析。为了将来这种悲剧不再重演，我们需要更多的郑念。 <br />
　　 <br />
　　　　Nien Cheng : Life and Death in Shanghai, Penguin Books, 1986. <br />
　　 <br />
　　　　《上海生死劫》程乃珊、潘佐君译，中国文化出版公司，1988年10月 <br />
上海生与死》，台湾：新出版，民76年。<br />
