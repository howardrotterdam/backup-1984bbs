郑念, 原名姚念媛，现居美国首都华盛顿。其著作Life and Death in Shanghai译为《上海生死劫》（程乃珊、潘佐君译,北京：中国文化出版公司，1988年10月;又译《上海滩的沉浮》，春风文艺出版社,《上海生与死》、《生与死在上海》等）。郑念一九一五年一月生在北京。中学就读于天津南开中学。大学读北平燕京大学。然后到英国留学于英国政治经济学院，后获该校硕士学位。在英国，郑念遇到其后来的丈夫，也是中国的留学生，在念研究生，比郑念大五岁。他们就在英国结婚。1935年她丈夫取得博士学位，1939他们回国的时候，抗日战争开始了，他们不能够回家了，因为他们的家都在北方：其丈夫是山东人，他妈妈在济南。所以他们就直接到了重庆。她丈夫在外交部供职，后来被派到澳大利亚任国民政府外交部派驻澳大利亚官员，在澳大利亚呆了七年。一九四八年十月一日，他们回到上海，那时候他们的孩子六岁。她丈夫是国民政府外交部上海办事处的主任。49年后曾任上海市政府的外交顾问。后来他离开政府成为英国壳牌石油公司（Shell International Petroleum Company）上海办事处的总经理，直到1957年死于癌症。 <br />
　　 郑念1957年到1966年在英国壳牌石油公司任职，1966年到1973年被关押在上海看守所，1980年9月27日取道香港后至加拿大，现定居美国。 <br />
　　 郑念的独生女儿郑梅萍死于文革迫害。妹妹姚念贻曾任上海电影译制厂配音演员。 <br />
