乍看书名，可能会给读者一个错觉：难道又是一本歌功颂德中国的书了？其实不然。相反地，作者英国伦敦政经学院国际事务、外交和重大政策研究中心研究员贾克，是依循「中国威胁论」，向世人提醒，称霸两百多年的「民主和自由的普世主义」，恐将因中国的「文明崛起」受到严厉挑战。届时，西方该如何自处？<br />
<br />
这本书凸显了西方的忧虑。贾克拥有如此的体悟，源自于他对东亚及中国的长期接触。其实，东亚对贾克而言，是一个又爱又恨的伤心地。他在马来西亚遇见了他已故马来籍的妻子。千禧新年，人在香港的妻子因癫痫发作送医，最后竟在华人医护的种族歧视下，延误治疗而死亡。<br />
<br />
丧妻之痛，让贾克陷入「最黑暗的日子」，以至于有五年时间，他决定重新出发，以历史文化角度，探索未来中国崛起后，究竟对世界产生怎样的影响？<br />
<br />
