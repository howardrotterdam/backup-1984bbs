													<h2>6: The Campaign Against Zhao</h2>
						
						
																											<div id="postmessage_105620" class="t_msgfont">The military &quot;victory&quot; over peaceful demonstrators in Tiananmen Square fails to deliver a sense of political victory. Party leaders, vilified around the world, move quickly to punish Zhao, convening an enlarged Politburo meeting before the end of June to make their accusations. Having been criticized for their handling of Hu Yaobang's dismissal two years earlier, Party leaders make a show of going through the proper steps this time. But Zhao points out the widespread violations of Party procedure and how he is the victim of Cultural Revolution-style tactics. He also reflects on the calculated risks he takes in sticking to his beliefs even as his colleagues tum against him.<br />
<br />
<br />
&nbsp; &nbsp; The Politburo held an enlarged meeting from June 19 to 21. First, Li Peng, representing the four members of the Standing Committee, set the tone of the proceedings by giving a report that accused me of having committed the serious errors of &quot;splitting the Party&quot; and &quot;supporting turmoil.&quot; He proposed that I be removed from my positions as General Secretary, Politburo member, and Politburo Standing Committee member. He also said that further investigations of me would be conducted.<br />
<br />
&nbsp; &nbsp; Afterward, the participants took turns speaking, each expounding on those criticisms. The most vicious and slanderous personal attacks came from Li Xiannian. At the beginning of the criticism meeting, Deng Xiaoping was absent. Chen Yun also did not appear, but provided a written statement containing two lines. It said that I had failed to meet the Party's expectations and that he supported the Party's decision to punish me. Wang Zhen's remarks were mainly about how Deng had been too lenient in punishing (Hu) Yaobang, allowing him to keep his membership on the Politburo and giving him a state funeral, thereby encouraging bourgeois liberalism.<br />
<br />
&nbsp; &nbsp; In the latter half of the last day of criticism speeches, Yao Yilin acted as chairman of the meeting. It seemed that they had no intention of letting me speak.<br />
<br />
&nbsp; &nbsp; The first time Wang Renzhong and Ding Guan'gen had come to my home, they had requested that I prepare a self-criticism. The second time they came, they had realized I was not going to write one, so they had tried to persuade me to remain silent. When the meeting was drawing to a close, I requested a chance to speak.<br />
<br />
&nbsp; &nbsp; He (Yao Yilin) looked at his watch and said, &quot;We've run out of time. If you must speak, keep it under ten minutes.&quot;<br />
<br />
&nbsp; &nbsp; I was very upset. I said, &quot;After all this time in session to deal with my case, after two whole days of criticisms, how can you now allow me so little time to respond?!&quot;<br />
<br />
&nbsp; &nbsp; Without waiting for his go-ahead, I began reading aloud a speech that I had prepared. I checked my watch afterward: it had taken me twenty minutes. In my speech I laid out the truth and the actual context of the debates and rebutted the accusations that had been made against me in the meeting. It came as a surprise to the meeting's participants. Some of them had intense expressions on their faces, appearing irritable and restless while I was talking.<br />
<br />
&nbsp; &nbsp; As soon as I finished speaking, Yao Yilin abruptly adjourned the meeting. I immediately left the scene. No one else moved. It was obvious that they had been instructed beforehand that they would be expected to express agreement with the displeasure with my speech and my attitude.<br />
<br />
&nbsp; &nbsp; The meeting resumed the next day. A vote was held to decide my case. They took out a statement that contained a resolution to strip me of all my official positions. Li Peng's original report and other people's speeches had all proposed dismissing me from my position as General Secretary and terminating my membership on the Politburo and Standing Committee, but preserving my membership on the Central Committee. But in this statement, my Central Committee membership was removed as well.<br />
<br />
&nbsp; &nbsp; It is obvious that after I'd delivered my speech the day before, they had all remained for a discussion and then determined that because of my bad attitude, a more severe punishment was appropriate. Since Deng Xiaoping and Chen Yun were not present during my speech, they must have reported to Deng and Chen afterward.<br />
<br />
&nbsp; &nbsp; In fact, I did not really care whether or not I was to keep my member removed, I had already been deprived of my right to chair Politburo meetings, which was passed to Li Peng. This was also illegal.<br />
<br />
&nbsp; &nbsp; What was especially ironic was that when voting was to begin, Deng Xiaoping actually said, &quot;All participants, whether a member of the Politburo or not, have the right to vote.&quot; At enlarged Politburo meetings, nonmember attendants are permitted to listen and to speak, but how can they be allowed to participate in the voting? Apparently, they wanted to rally more support. Li Xiannian explained that this right to. vote was being granted by Li Peng, the chairman of the meeting. This was completely rule by force! What Part Charter or rules were they following?! The elders, long accustomed to the Party's custom of &quot;acknowledging neither laws nor heavenly constraints,&quot; of course, were not concerned.<br />
<br />
&nbsp; &nbsp; Now I have spoken of this matter; I don't know how this will be recorded in the Party's history.<br />
<br />
&nbsp; &nbsp; A Central Committee meeting was held from June 23 to 24 to pass the political and administrative judgment made against me at the enlarged Politburo meeting. I was notified about the meeting and attended the group to which I'd been assigned, the North China Group. I listened as a few comrades criticized me, then I spoke briefly.<br />
<br />
&nbsp; &nbsp; I said, &quot;Thank you all for your advice. I have a written statement that is a revised version of the statement I prepared for the enlarged Politburo meeting. I have already submitted it to the Service Division. I hope copies of my written statement will be distributed to all comrades in attendance.&quot;<br />
<br />
&nbsp; &nbsp; (Director of the Propaganda Department) Wang Renzhi was also in this group, and said that the Central Committee had agreed to distribute copies of the statement to all the participants. But, in fact, the statement was passed out to participants only as the meeting was about to end, and then quickly retrieved. However, (Beijing mayor) Chen Xitong and (State Education Commission director) Li Tieying's rebuttal of my statement had been distributed earlier. So, in the meeting, it was a bizarre situation in which participants were reading the criticism of my statement without having seen my statement, then were finally shown my statement toward the end of the meeting only to have it immediately retracted again. As a<br />
result, I'm afraid many people had to rush through my statement, or did not get a chance to read it at all.<br />
<br />
&nbsp; &nbsp; So-called &quot;background information&quot; about June Fourth was also distributed, in the name of the General Office. It amassed a large quantity of material from around the country and overseas, implying that I was a conspirator representing counterrevolutionary forces in the country and overseas aimed at overthrowing the Chinese Communist Part and Deng Xiaoping. It also included material making false accusations that my staff cooperated with the students, sent information to them, and revealed the military secret of the plan to impose martial law. It was obvious that the point of printing such &quot;background material&quot; was to create a general impression that I was indeed guilty of the most heinous crimes and was unpardonably wicked.<br />
<br />
&nbsp; &nbsp; They sought to completely destroy my political and moral standing. Some of the speeches delivered at the meeting were entirely in the style of the Cultural Revolution: reversing black and white, exaggerating personal offenses, taking quotes out of context, issuing slanders and lies-all in Cultural Revolution language. At the time, I thought to myself, if records<br />
of this meeting were not clearly marked &quot;CCP Fourth Plenum of the Thirteenth Part Congress,&quot; one could easily have mistaken them for documents from the Cultural Revolution.<br />
<br />
&nbsp; &nbsp; According to the Part Charter, the dismissal of a member of the Central Committee requires a two-thirds majority in the plenum to pass. It was obvious that the top leaders were not confident they could achieve that. If secret voting were to take place, it was possible that they would not obtain the necessary two-thirds majority. Instead they abandoned secret voting and called for voting by a show of hands. Obviously, in that kind of atmosphere and under that kind of pressure, by having to publicly raise one's hand, a lot of people felt unable to vote according to their real opinions. With everyone watching and cameras rolling, some people were forced to raise their hands even if they were opposed. Therefore, the resolution was unanimously passed.<br />
<br />
&nbsp; &nbsp; I must point out that in the past, whether for General Secretary or for Politburo or Politburo Standing Committee memberships, all elections were carried out through secret voting. Voting in this way, with a show of hands, was quite abnormal. In that kind of atmosphere, under that kind of pressure, and with investigations of events and people connected to me already under way, how could people feel free to express their opinions while raising their hands?<br />
<br />
&nbsp; &nbsp; By insisting on my view of the student demonstrations and refusing to accept the decision to crack down with force, I knew what the consequences would be and what treatment I would receive. Mentally, I was fully prepared. I knew that if I persistently upheld my view, I would ultimately be compelled to step down. I had already considered this. If I wanted to keep my position, or give up my post in some face-saving way, I would have to give up my viewpoint and conform. If I persisted, then I had to be prepared to step down.<br />
<br />
&nbsp; &nbsp; After repeated and careful consideration, I had decided I would rather step down than conform to their view. I had spoken with my wife and children at home about what I was thinking, and had asked them to be prepared.<br />
<br />
&nbsp; &nbsp; I was also mentally prepared for the consequences of my speech at the enlarged Politburo meeting. I thought I might be expelled from the Party, since it was necessary for a person who had committed an error to hang his head in guilt in order to be judged as having the correct attitude. I thought that probably I would not be put in prison, since I had done nothing wrong. For people like me who had had some amount of influence at home and abroad, they could not possibly manage to conduct an absolutely secret trial. Hence I determined that stepping down was a certainty, expulsion from the Part was the worst that might happen, and imprisonment was unlikely.<br />
<br />
&nbsp; &nbsp; Under a political system such as ours, it made very little difference whether I remained in the Party or was expelled from it. People in my situation who have retained Party membership don't have the normal rights of membership anyway. Furthermore, expulsion from the Party would not affect my beliefs and ideals.<br />
<br />
&nbsp; &nbsp; After the Fourth Plenum of the 13th Central Committee, Cultural Revolution-style tactics that had been condemned and abandoned long ago were taken up to be used against me. These tactics included inundating the newspapers with critical articles making me out to be an enemy, and casual disregard of my personal freedoms. Immediately after the Cultural Revolution, having learned from its painful experiences, the Party had passed a new Part Charter at the 12th Party Congress (1982), &quot;Several Rules Governing Political Life in the Party.&quot; The rules were aimed at preventing the Cultural Revolution from ever happening again.<br />
<br />
&nbsp; &nbsp; After June Fourth, they entirely disregarded these rules in their treatment of me, instead openly violating them and reassuming the ultraleft tactics of the Cultural Revolution. This was something I had not anticipated.</div>

							
							
							
							
