<p align="center"><strong><font size="7">Prisoner of the State</font></strong><br />
<br />
<font size="5"><strong>The Secret Journal of Zhao Ziyang</strong></font><br />
<br />
Translated and Edited by Bao Pu, Renee Chiang, and Adi Ignatius<br />
Foreword by Roderick MacFarquhar</p><br />
<br />
<br />
<br />
How often can you peek behind the curtains of one of the most secretive governments in the world? Prisoner of the State is the first book to give readers a front row seat to the inner workings of China. It is the story of Premier Zhao Ziyang, the man who brought liberal change to that nation and who, at the height of the Tiananmen Square protests in 1989, tried to stop the massacre and was dethroned for his efforts.<br />
<br />
When China's army moved in, killing hundres of students and other demonstrators, Zhao was placed under house arrest at his home in Beijing. China's most promising advocate for change had been disgraced, along with the policies he stood for. The Premier spent the last 16 years of his life, up until his death in 2005, in seclusion. An occasional detail about his life would slip out: reports of a golf excursion, a photo of his aging face, a leaked letter to China's leaders. But China scholars often lamented that Zhao never had his final say.<br />
<br />
As it turns out, Zhao did produce a memoir, in complete secrecy. He methodically recorded his thoughts and recollections on what had happened behind the scenes during many of modern China's most critical moments. The tapes he produced were smuggled out of the country and form the basis for Prisoner of the State. In this audio journal, Zhao provides intimate details about the Tiananmen crackdown; he describes the ploys and doublecrosses China's top leaders use to gain advantage over one another; and he talks of the necessity for China to adopt democracy in order to achive long-term stability.<br />
<br />
The China that Zhao portrays is not some long-lost dynasty. It is today's China, where the nation's leaders accept economic freedom but continue to resist political change.<br />
<br />
If Zhao had survived - that is, if the hard-line hadn't prevailed during Tiananmen - he might have been able to steer China's political system toward more openness and tolerance.<br />
<br />
Zhao's call to begin lifting the Party's control over China's life - to let freedom into the public square C is remarkable coming from a man who had once dominated that square. Although Zhao now speaks from the grave in this moving and riveting memoir, his voice has the moral power to make China sit up and listen.<br />
