													<h2>2: An Editorial Makes Things Worse</h2>
						
						
																											<div id="postmessage_104746" class="t_msgfont"><a href="https://docs.google.com/Doc?id=dmrm5wh_66h48nphr&amp;hl=en#2:_An_Editorial_Makes_Things_Worse" target="_blank">https://docs.google.com/Doc?id=d ... _Makes_Things_Worse</a><br />
<br />
&nbsp; &nbsp; The Communist Party leadership doesn't know how to respond to the growing student protest. When Zhao leaves on his trip to North Korea, hard-liners opposed to his reforms take advantage of his absence and maneuver supreme leader Deng Xiaoping to their side, leading him to angrily denounce the demonstrations. <br />
<br />
 <br />
&nbsp; &nbsp; Any hope of calming things down is lost on April 26, when the Party issues its official verdict on the protests in an editorial in the People's Daily that reports Deng's harsh words. Deng is shocked to learn that his comments have been published, but withdrawing the piece would imply that China's supreme leader had made a mistake, a path the Party doesn't wish to risk. The Party and the protesters are now locked on a collision course. Zhao has failed to sense the danger before leaving for Pyongyang. <br />
<br />
&nbsp; &nbsp; So why did the student demonstrations later turn into such a mess? <br />
 <br />
&nbsp; &nbsp; The crux of the situation was the April 26 editorial. The students had feelings of dissatisfaction that, one way or the other, they were going<br />
to express. If they had not held demonstrations then, they would have held them later. They were truly discontented!<br />
<br />
 <br />
&nbsp; &nbsp; However, the scale of the demonstrations, the mess it turned into, and why it happened when it did were all the results of the April 26 editorial.<br />
The situation before the publication of the editorial and the situation afterward were different. If the right measures had been taken to direct the situation, then there would not have been such dire results. I visited Deng Xiaoping on April 19 to discuss my Nort Korea trip, to talk to him about the student demonstrations, and to give him my views on how the situation should be handled. At the time, Deng had expressed support for me. Yet things took a strange turn after that. <br />
 <br />
&nbsp; &nbsp; The very evening of the day that I left Beijing, Li Ximing and Chen Xitong of the Beijing Part Committee asked (chairman of the National People's Congress Standing Committee) Wan Li to call a meeting of the Politburo Standing Committee to listen to their report. Wan Li fell for their trick. (Wan Li and I had been in total agreement in our view of the student protests.) Wan Li directed their request to Li Peng, as Li Peng was temporarily in charge of Standing Committee* activities while I was abroad. The very next evening, Li Peng called for a Standing Committee meeting.<br />
 <br />
<br />
* The author often shortens &quot;Politburo Standing Committee&quot; to &quot;Standing Committee&quot;.<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; With Li Peng presiding, Li Ximing and Chen Xitong vigorously presented the student demonstrations as a grave situation. They disregarded the fact that the student demonstrations had already calmed down. In fact, student opinions had begun diverging.<br />
<br />
&nbsp; &nbsp; Some of the students believed that they should resume classes and had already done so, while a minority opposed the return to classes. Internal friction had become apparent in some schools. Some of the students had attempted to resume classes, while other, more extreme students had blocked the entrances to the classrooms to prevent them from entering. This shows that for some students, the activities had not fully satisfied their need to vent their anger. If measures were to be taken to reduce tensions, to have dialogue, and to allow students the chance to propose certain reasonable requests, this was a good time to do so. <br />
<br />
&nbsp; &nbsp; However, in their report, they (Li Ximing and Chen Xitong) went so far as to state, &quot;Nationwide, large-scale demonstrations including the participation of high school students and workers are being organized and are fomenting.&quot; They also reported that &quot;university students in Beijing<br />
have sent contacts to places around the country and have conducted fund-raising in the streets to prepare for activities on a larger scale.&quot; They<br />
denounced the extreme opinions of a few students, especially remarks directed specifically at Deng Xiaoping. They presented the demonstrations<br />
as opposing the Communist Part and targeting Deng Xiaoping personally.<br />
<br />
&nbsp; &nbsp; With the onset of reform, students, especially college students, had been exposed to many Western ways. Remarks critical of political leaders were made casually and considered inconsequential; the intense climate (of fear) that existed during the Cultural Revolution* and before no longer existed. Many of these student remarks targeted me, such as those that accused my children of making business deals utilzing official resources or those that claimed that trainloads of fertilzer had been sent to my hometown. <br />
* The Great Proletarian Cultural Revolution was a period of great upheaval in China that lasted from 1966 to 1976. Launched by Chairman Mao Zedong, who was frustrated by the passive resistance of his own bureaucracy to his radical economic policies, the ultraleftist campaign led to the persecution of milions, including purges of hundreds of thousands of Communist Part officials.<br />
<br />
&nbsp; &nbsp; With hundreds of thousands of people involved, it's impossible for there to have been no extreme or one-sided comments. Things appear extremely grave if you select only the ten most extreme statements being expressed by all of the people involved. I am not sure what was behind Li<br />
Ximing and Chen Xitong's behavior: either their old mentality of class struggle was at work or they had other ulterior motives. The student demonstration was deemed an &quot;organized and carefully plotted political struggle,&quot; and was documented as such in the minutes of the meeting. Li Peng, Li Ximing, and Chen Xitong were the ones initially responsible for this. On April 25, Li Peng and (President) Yang Shangkun reported to<br />
Deng Xiaoping about the Politburo Standing Committee meeting. Deng Xiaoping had always tended to prefer tough measures when dealing with student demonstrations because he believed that demonstrations undermined stabilty. After listening to their report, Deng immediately agreed to<br />
label the student demonstrations &quot;anti-Part, anti-socialist turmoil&quot; and proposed to resolve the situation quickly, in the manner of &quot;using a sharp<br />
knife to cut through knotted hemp.&quot; <br />
 <br />
&nbsp; &nbsp; When I had visited him on April i 9, he had agreed with my position. On 'the 25th, after being briefed by Li Peng and Yang Shangkun, he had changed his mind to agree with their assessment. After all, it coincided more closely with what he had really believed all along. Deng's discussion with Li Peng and others on April 25 was supposed to be an internal affair. However, Li Peng decided to disseminate the contents of Deng's remarks that very evening to Part cadres of all levels, and paraphrased their talk in the editorial that he had the People's Daily publish on April 26, publicly designating the student demonstrations as &quot;premeditated and organized turmoil with anti-Part and anti-socialist motives.&quot; <br />
 <br />
&nbsp; &nbsp; Before my visit to North Korea, neither Li Peng nor the cadres in Beijing mentioned these viewpoints to me. Immediately upon my leaving Beijing, they quickly held a Politburo Standing Committee meeting and gained support from Oeng Xiaoping. This constituted a departure from the previous position and the principles adopted by the Standing Committee.<br />
<br />
&nbsp; &nbsp; Deng was not happy about how Li Peng had made his remarks public. Deng's children were also displeased that Deng had been put in the position of being in direct confrontation with the public. As I was preparing a speech for the commemoration of the May Fourth Movement, * (Deng's daughter) Maomao called (Zhao adviser) Bao Tong, who was drafting the text, to suggest that the speech include remarks about how much Deng loved and protected young people.<br />
* The May Fourth Movement is the name given to nationwide demonstrations staged in 1919 that were provoked by the Treaty of Versailes, which was perceived as unfair to China. The demonstrtions marked the shiftng of the modern Chinese intellectual movement away from Western liberalism, toward the ideals of the Russian Revolution. China's Communist Part identifies the movement as its intellectual origin.<br />
&nbsp; &nbsp; Later, on May 17, at the meeting at Deng's home in which the decision was made to impose martial law, Deng demanded of Li Peng, &quot;Don't repeat what you did before; don't reveal that it was I who made the decision to impose martial law!&quot; Li Peng said repeatedly, &quot;I won't! I won't!&quot; It was obvious that some people were attempting to use the extreme words of a few students to aggravate the situation and push the government to the point of direct confrontation. With the implementation of reform, it should not have been such a big deal that students criticized leaders. They were just expressions of frustration and were not a challenge to our entire political system.<br />
<br />
&nbsp; &nbsp; However, selectively gathering all the personal criticisms and reading them aloud to Deng made for a tremendous insult to the old man. These people selected sporadic extreme opinions of a tiny minority of students and represented them as the major trend of the movement, which they claimed was directed specifically against Deng Xiaoping himself. Deng tended to think in a certain way that was formed during the years when class struggle was the primary objective, so as soon as he heard Li Peng's report, he reacted accordingly. I am afraid this is one of the major reasons for his decision.<br />
<br />
&nbsp; &nbsp; While I was in North Korea, the minutes of the Standing Committee meeting of April 24 and Deng Xiaoping's remarks reached me through the embassy. I replied by telegram: &quot;I completely agree with Comrade Deng Xiaoping's decision regarding the policy toward the current turmoil.&quot;<br />
 <br />
&nbsp; &nbsp; When I received these documents, I had to respond, and I was not in any position to express disagreement because I was abroad and had no direct knowledge of the situation at home. However, I did not express my views on the minutes of the Standing Committee meeting. Upon reading<br />
Oeng Xiaoping's remarks, I did not think that any immediate actions would be taken against the students. My first thought was that another<br />
campaign against liberalism might begin, possibly on an even greater scale than before (it hadn't occurred to me that the student protests would<br />
not subside, because I had not thought of them as a major problem). (A new campaign could) damage the momentum that the reforms had gained<br />
since the 13th Part Congress (held in OctoberlNovember i 987) especially in political reform. That's because Deng believed that the student demonstrations were the long-term results of the lax execution of the Anti-Bourgeois Liberalization Campaign. <br />
 <br />
&nbsp; &nbsp; However, after the publication of the April 26 editoriaL, the situation immediately changed, and the confrontation escalated. Students were<br />
angered by the editorial's wording and political accusations. &quot;Anti-Part,&quot; &quot;anti-socialist,&quot; &quot;premeditated plot,&quot; etc., were terms that had not been<br />
heard in years, so they provoked intense emotions. Those who were moderate before were then forced to take sides with the extremists. <br />
 <br />
&nbsp; &nbsp; After I returned from North Korea, I invited several people from universities over for discussions. Ali of them talked about this situation. Upon the publishing of the April 26 editorial, many people were highly displeased, including those in various government departments. Many exclaimed, &quot;How did we end up with that thing?!&quot;<br />
<br />
&nbsp; &nbsp; The number of demonstrators on the streets on April 27 had swelled to ten thousand. The harsh words of the editorial made students feel that their actions might lead to a crackdown. Some even left wils and letters of farewell for their familes before taking to the streets. <br />
 <br />
&nbsp; &nbsp; The April 26 editorial not only agitated the students, but also left those in various government departments, organizations, and other political parties in a general state of discontent. They found it incomprehensible and were displeased or even angered by it. They believed that the students had acted out of a sincere concern for important matters of state and the fate of reforms, and had expressed their views on some hot social issues, all out of goodwil and patriotism. The government not only failed to express support or provide guidance, but with the harshly worded editorial took a stand in opposition to the students, labeling them with the political tags &quot;anti-Part&quot; and &quot;anti-socialist.&quot; The reaction from intellectuals was especially critical.<br />
 <br />
&nbsp; &nbsp; The government's response boosted popular sympathy and support for the students. Video recordings showed that wherever the students went, crowds lining their passage applauded and welcomed them. Some even joined in the protests. Even the police who had lined up to form a blockade made only superfcial attempts to stop them, and then let everyone pass. Some of the prepared roadblocks were opened up as soon as the students arrived, as if they'd never meant to stop them in the first place.<br />
<br />
&nbsp; &nbsp; Many senior cadres grew quite worred about the student demonstrations. After Deng Xiaoping's remarks, they were afraid that the escalating confrontation would result in bloodshed. Again and again, they warned the Central Committee to show restraint and to avoid using force. (Influential Part elder) Peng Zhen phoned the Central Committee's General Office directly several times to say that under no circumstances should force be used. He hoped the Central Committee would not aggavate tensions.<br />
<br />
&nbsp; &nbsp; One exception was (Part elder and chairman of the Chinese People's Political Consultative Conference) Li Xiannian, who after hearing Deng's remarks, phoned Deng and said, &quot;We must make the decision and be prepared to arrest hundreds of thousands of people!&quot; I admit I can't attest to the accuracy of this. (Another Part elder and vice president of China) Wang Zhen also proposed arresting more people. <br />
 <br />
&nbsp; &nbsp; Faced with tens of thousands of demonstrators and the entreaties of all these senior cadres, those who had been determined to quell the demonstrations, such as the Beijing Part Committee and Li Peng, were suddenly at a loss as to how to proceed. This was certainly a positive thing.<br />
The students had anticipated a crackdown, but when it didn't happen they returned to their schools celebrating their victory and were left feeling more encouraged and fearless than ever. <br />
 <br />
&nbsp; &nbsp; Because Deng's remarks had been sent to school administrations and the editorial had been published, many members of Part organizations, university presidents, and teachers had initially made intensive efforts to prevent students from taking part in the demonstrations, pleading with them not to take to the streets. When the students returned unharmed, these people felt humiliated. They did not like feeling that they had been misled. They had put themselves out for nothing.<br />
<br />
&nbsp; &nbsp; (Beijing mayor) Chen Xitong and many others like him shared this feeling. At the Politburo Standing Committee meeting on May 1, Chen Xitong was full of anger as he presented his report from the Beijing Part Committee. He said that the school officials all feltas though they'd been &quot;sold out.&quot; I condemned his remarks and asked him, &quot;Who has sold out whom?&quot;<br />
<br />
&nbsp; &nbsp; The large-scale demonstrations of April 27 made a few things clear. The original intention of the April 26 editorial's designàtions &quot;anti-Party, anti-socialist&quot; was to deter the students. The result, however, was the opposite: the demonstrations had grown bigger. This showed that the old ways of political labeling that had worked before were no longer effective.<br />
<br />
&nbsp; &nbsp; Second, since Deng Xiaoping's internal remarks of April 25 had been disseminated widely, the students were aware that Deng was in support of the editorial. They went out to protest anyway, proving that even the symbol of the paramount leader had lost its effectiveness.<br />
<br />
&nbsp; &nbsp; Third, the Beijing Municipal Government had just announced a new regulation for demonstrations that imposed strict limits and countermeasures, but this had also been ignored, making the new regulation as good as a piece of wastepaper. Even the police blockades had failed. <br />
 <br />
&nbsp; &nbsp; Once I'd grasped the circumstances after my return to Beijing, I realized that if the situation were to continue without a reduction in tensions, a violent solution was almost a certainty. The situation now was entirely different from what it was before April 27, because the students had grown fearless. They believed that the government had already used all the means at its disposal, all of which had proven ineffective, leaving only the mobilzation of the miltary. Yet the students could not imagine that the government would actually mobilze the army against them.<br />
<br />
&nbsp; &nbsp; When I passed through (the northeast city of) Shenyang on my way back from North Korea, I was given a report on the responses of Shenyang officials to Deng Xiaoping's remarks. They had expressed doubts: &quot;Can measures of this kind still be used?&quot; They told me that many people were critical of Deng after hearing his remarks. Hence, upon my return from North Korea, the situation had grown perilous. Large-scale bloodshed had become all too possible.</div>

							
							
							
							
