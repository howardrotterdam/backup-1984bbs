													<h2>5: The Accusations Fly</h2>
						
						
																											<div id="postmessage_105619" class="t_msgfont">Zhao is purged from his leadership role as Party elders close ranks to oppose him. Zhao argues that their tactics violate Party regulations, but he is powerless to fight back. Yet just as he refuses to sign off on the decision to bring the army into Beijing, he declines the Party's demand to make a &quot;self-criticism&quot;-an important tool in the Party's efforts to maintain one official version of the truth. Zhao does express concern about how a comment he made to Mikhail Gorbachev was misconstrued as a veiled attack on Deng.<br />
<br />
<br />
&nbsp; &nbsp; I want to raise another issue here, that is, the unfair treatment that I received because of the political unrest in Beijing. I had refused to attend the meeting of May 19 that announced martial law. This made Deng and the other elders extremely angry. On the 20th, Deng called Chen Yun, Li Xiannian, Wang Zhen, Peng Zhen, Yang Shangkun, Li Peng, Qiao Shi, and Yao Yiln for a meeting at his house. Of course, I was not informed. They did not notify Hu Qili, either, so he did not attend.<br />
<br />
&nbsp; &nbsp; I hear that in the meeting, Wang Zhen furiously vilified me as being counterrevolutionary. Li Xiannian accused me of setting up &quot;second headquarters.&quot; In the end, Deng decided to remove me from the post of General Secretary, but added that the announcement to the public should be delayed until after the completion of some necessary procedures. I was brushed aside just like that.<br />
<br />
&nbsp; &nbsp; This was not a Politburo Standing Committee meeting, since only three of its five members were in attendance. Neither Hu Qili nor I had been removed from our posts before the meeting began, so we were still members of the PSC. In my opinion, it cannot be considered legal to have made such a decision when two members of the PSC had not even been notified.<br />
<br />
&nbsp; &nbsp; I took a three-day leave, from the 19th to the 21st. Nobody actually told me that I had been removed from my position. Of course, nobody contacted me on any work-related issues, either. Essential communication channels had been cut off, and I had been isolated. I heard through other channels that Li Peng, Yang Shangkun, Yao Yiln, and (Director of Organization Department) Song Ping each held meetings with various departments announcing my &quot;crime.&quot; They also organized working groups and drafted documents to prepare for an upcoming Central Committee meeting at which they planned to announce my case. Meanwhile, they assembled in Beijing the first- and second-rank leaders of all the provinces and municipalities to brief them.<br />
<br />
&nbsp; &nbsp; Through all these important arrangements, the Politburo did not hold a single meeting; nor did the Politiburo Standing Committee make any decisions. The Standing Committee was made up of five members; with Hu Qili and me excluded, there could be no legitimate PSC meeting. All these arrangements were lacking in legal authority.<br />
<br />
&nbsp; &nbsp; The Party Charter lays out these rules: &quot;When the Central Committee is not in session, the Politburo assumes power on its behalf. . . . Meetings of the Politburo are to be chaired by the General Secretary.&quot; It is obvious that none of these arrangements were made through Politburo meetings, and of course they were not chaired by me. Therefore, no matter what organization held meetings, or who chaired them, they were all in violation of the Part Charter.<br />
<br />
&nbsp; &nbsp; Under these circumstances, in which no one had announced that I had been removed from my post, yet I was unable to use my authority, I worried that I would ultimately be accused of having abandoned my post. Therefore, I talked to (director of the Part's General Office) Wen Jiabao to suggest a Politburo meeting. Wen Jiabao replied that, in fact, the Central Committee General Office had been brushed aside as well. All arrangements had been made by Li Peng and Yang Shangkun, bypassing the General Office. He said that if I really wanted to call a meeting, the General Office would send out the notice, but he believed that the consequences would not be good and hoped I would carefully reconsider.<br />
<br />
&nbsp; &nbsp; Since I could not call for a meeting, I asked my secretary to phone Yang Shangkun to ask him over for a talk. My intention was to ask him to clarify whether I had already been removed from my position. I also wanted to explain to him why I had talked to Gorbachev about Deng Xiaoping's position within the Part (Zhao expounds on this in Chapter 7).<br />
<br />
&nbsp; &nbsp; On June 2, (Vice Chairman of the Chinese People's Political Consultative Conference) Wang Renzhong and (Vice Minister of State Planning) Ding Guan'gen came to my house and said that in response to my request to speak to Yang Shangkun, the two of them had been sent by the Central Committee and entrusted with this discussion. They said that the Central Committee was soon to hold Politburo and Central Committee meetings to deal with my case and that I should carefully consider preparing a self-criticism.<br />
<br />
&nbsp; &nbsp; I started off by explaining to them my discussion with Gorbachev. Then I raised the issue of how the Central Committee's organizations could be functioning when two of the five members of the Standing Committee had been pushed aside. Who was participating in the meetings? Wang Renzhong said there had been no Standing Committee reelections,<br />
nor had there been any meetings held of late.<br />
<br />
&nbsp; &nbsp; I said that having taken three days' sick leave, I could understand if I was not permitted to resume work. I had no problems with being asked to stand aside, but I should not later be accused of having neglected my work and abandoning my post. That was the reason I had asked to talk with Yang Shangkun. With regards to preparing a self-criticism, I said that I had not been told anything. Criticisms of me were being made everywhere without attempts to check with me about the facts. Documents of criticisms were circulating all over the place, but none had been shown to me. How could I write a self-criticism under such conditions? If I were to be given a chance to speak in the future on issues that I admitted had been in error, I would make a self-criticism.<br />
<br />
&nbsp; &nbsp; It was a long talk, lasting more than two hours. I did most of the talking. I spoke about the conditions and my views on the April 26 editorial, the speech on May 3 to the youth delegates, the May Fourth speech at the ADB meeting, and my refusal to attend the May 19 meeting to announce martial law.<br />
<br />
&nbsp; &nbsp; And lastly, I strongly protested the way in which they had detained Bao Tong. On May 28, Bao Tong had been called in by the Department of Organization for a talk, from which he never returned. Meanwhile, they searched his office. I had immediately asked my secretary to call (Director of Organization Department) Song Ping to voice my protest. To Wang Renzhong and Ding Guan'gen I said, &quot;If they believe Bao Tong has done anything wrong, the appropriate Part organization should conduct an investigation, but they must proceed according to the Part Charter and the law. Part organizations, much less the Department of Organization, have no authority to deprive him of his personal freedom. We are now in<br />
the 1980s; we cannot use these old methods of past political campaigns.&quot; I demanded that they relay my message to the Central Committee.<br />
<br />
&nbsp; &nbsp; In their assessment of this talk, they deemed my attitude to have been very bad indeed. Wang Renzhong and Ding Guan'gen returned to my home on June 17. They said that on June 19 the Central Committee would hold a Politburo meeting to deal with my case, and they requested that I appear modest, show restraint, and keep calm even if some of the elders used harsh words. I could choose to speak or remain silent, but I was not to argue excessively.<br />
<br />
&nbsp; &nbsp; I replied, &quot;If this is a meeting to deal with my case, I must be given the chance to speak freely.&quot;<br />
<br />
&nbsp; &nbsp; Ding Guan'gen also asked me to reflect seriously on my faults and adopt a proper attitude for the meeting. Wang Renzhong revealed that internally they had decided to maintain my Central Committee membership and Hu Qili's Politburo membership. * He also said that they had already relayed my opinion of Bao Tong's &quot;isolation and investigation&quot; to the Central Committee; Bao Tong was now under &quot;surveillance and house arrest,&quot; which (they said) conforms with proper legal procedures.<br />
<br />
&nbsp; &nbsp; * Hu Qili, who had sided with Zhao in taking a soft line toward the student demonstrations, was also purged from the top ranks of the Part, losing his slot on the elite Politburo Standing Committee.<br />
<br />
<br />
&nbsp; &nbsp; It seems the purpose of their visit was: one, to notify me about the upcoming meeting, and two, to persuade me not to stage a challenge, or to keep my arguments to a minimum. When Wang Renzhong and Ding Guan'gen first came to my house on June 2 to inform me of the meeting arranged to deal with my case, they said that Deng Xiaoping had mentioned<br />
that the handling of Hu Yaobang's case had resulted in criticisms both at home and abroad, so this time with Zhao's case, we must follow proper procedures. He directed them to prepare proper documentation; as soon as these documents were ready, a meeting would be held.<br />
<br />
&nbsp; &nbsp; It was all a terrific irony. In fact, I had already been detained and isolated by them without justification or legality. First they illegally deposed me from my position as General Secretary, then they claimed to be in accordance with procedures. This shows that they were apprehensive; afraid of criticism from others.<br />
<br />
&nbsp; &nbsp; They would have held the meeting earlier, but it was delayed by the events of June Fourth.</div>

							
							
							
							
