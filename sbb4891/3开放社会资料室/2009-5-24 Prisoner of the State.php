													<h2>3: Power Struggle</h2>
						
						
																											<div id="postmessage_104785" class="t_msgfont">As the protests escalate, the political stakes get higher. Zhao returns to Beijing and tries to calm things down. Soviet leader Mikhail Gorbachev'spending visit to China gives protesters leeway, since the Party isn't likely to crack down violently on the eve of this trip. Hard-line Premier Li Peng opposes Zhao's effort to deal leniently with the situation, and both sides try to win over paramount leader Deng Xiaoping. Tensions escalate when Shanghai offcials shut down a bold newspaper that they feel has gone too far in its reporting on the protests. <br />
&nbsp; &nbsp; I have described above how, during my visit to North Korea, the guidelines laid out to deal with the student demonstrations were changed by Li Peng and others at home. Now I wil address the struggle between the two sharply conflcting approaches to the student demonstrations that occurred after my return from North Korea. <br />
 <br />
&nbsp; &nbsp; Li Peng's decision to disseminate Deng Xiaoping's remarks on April 25 and 26 throughout Beijing and down to local administrative levels resulted<br />
in many criticisms of Deng. This really upset Deng and his family. Deng's family accused Li Peng of having abruptly pushed Deng to the front lines while he himself played the good guy.<br />
 <br />
&nbsp; &nbsp; Given the above situation, and because the editorial provoked large demonstrations on April 27 and widespread criticism, Li Peng felt pressured to ask (Political Secretary of the Politburo Standing Committee) Bao Tong to draft an editorial on April 29 and to request that (State Council spokesman) Yuan Mu and (State Education Commission Vice Minister) He Dongchang hold a dialogue with the students.<br />
<br />
&nbsp; &nbsp; During the resulting dialogue session, they (Yuan Mu and He Dongchang) responded positively to many of the students' pleas, conceded that many of the students' objectives were the same as those held by the Part and the government, and explained that the editorial was not directed against the students. They even declared that 99.9 percent of the students were good, with only a tiny minority being anti-Part and antisocialist. These were the measures taken to calm the students.<br />
<br />
&nbsp; &nbsp; At the same time, they were extremely worred that the April 26 editorial might be overturned, and were especially afraid that I Would not support their actions upon my return. (Director of the United Front Work Department) Yan Mingfu reported to me that Li Peng had told him that if, upon my return, I did not support the April 26 editoriaL, Li would have no choice but to resign. Li Peng and (Politburo Standing Committee member) Yao Yiln colluded with each other to persuade me to express my support. They repeatedly requested that I add phrases such as &quot;opposing turmoil&quot; and &quot;opposing bourgeois liberalization&quot; into the speech I was preparing for the commemoration of the May Fourth Movement. When the draft was sent to them for comments, Li and Yao both requested the addition of remarks condemning bourgeois liberalization.<br />
<br />
&nbsp; &nbsp; Furthermore, because of the wide dissemination of Deng Xiaoping's remarks, Deng felt that his image among young people had been damaged. Deng Rong (Deng's daughter, also known as Maomao) told me through Bao Tong that references to Deng loving and protecting youths must be added to the speech. Under the circumstances, I did indeed decide to add to the speech one paragraph dedicated to how much Deng loved and protected youths.<br />
<br />
&nbsp; &nbsp; As soon as I had returned from North Korea, on the morning of April 30, Li Peng rushed over anxously to get me to call a meeting so I could listen to the report of the Beijing Part Committee. His goal was to pressure me to express support for the actions they had already taken. <br />
 <br />
&nbsp; &nbsp; By May 1, at a gathering of Standing Committee members, I was already aware of thestTOng reactions against the April 26 editoriaL. However, since I myself stil knew very little about the actual situation and also to avoid a sudden reversal in policy, I did have to express some kind<br />
of approval of Li Peng's work, at least in some vague way.<br />
<br />
&nbsp; &nbsp; However, I emphasized that it was critical to win over the support of the mainstream. We had to distinguish the tiny minority from the mainstream, and not push the majority of people over to the opposing side. That is, we should not create a situation in which the bulk of the populace felt we were trying to repress them. No matter what the reason, we had to calmly acknowledge the fact that the view expressed by the April 26 editorial was widely divergent from the view held by the vast majority of people, especially students, intellectuals, and other political parties. I pointed out the need to conduct a wide range of dialogues. Not only should we meet with and seek the opinions of students, but also teachers and workers.<br />
<br />
&nbsp; &nbsp; As for the designation of the nature of the events, I emphasized that we could give new explanations that built upon the wording of the April 26 editorial, by indicating that only a tiny minority was .actually anti-Party, anti-socialist, and pushing for chaos. I hoped to mitigate the effects of the April 26 editorial. I also pointed out that we must advocate a return to classes because this was agreeable to the students' parents, their teachers,<br />
and most of society. So long as classes were resumed, the situation could be stabilzed and emotions would have a chance to cool down. Then all other matters could eventually be resolved.<br />
<br />
&nbsp; &nbsp; Once back from North Korea, I tried to garner information from all sides. I first called for the visual recordings of the demonstrations of April 27. On May 2, I responded to requests from leaders of other political parties-Fei Xiaotong, Sun Qimeng, and Lei Jieqiong-to convene a session to discuss the student demonstrations. On the morning of May 5, I asked the president of Peking University, Ding Shisun, and the vice president of Beijing Normal University, Xu Jialu, for a meeting. I asked them for a synopsis of the situation in their schools and for their assessments. In the afternoon, I invited myself to a discussion being held by the Central Committee of the China Democratic League for the university staff members within their organization.<br />
<br />
&nbsp; &nbsp; After gathering information and assessing the situation, I believed even more strongly that the student demonstrations had gained widespread sympathy from all comers of society and that the April 26 editorial and the way that the Central Committee had handled the demonstrations were in contradiction to the wishes of the people. If no measures were taken to ease the, tensions caused by the April 26 editorial, students would continue to fear that they were being threatened with retaliation, and tensions would continue unabated.<br />
<br />
&nbsp; &nbsp; I also felt that if the student demonstrations could be resolved along the principles of democracy and law, through dialogue and an easing of tensions, it could possibly boost China's reform, including political reform. On the other hand, if we were to suppress the demonstrations with violence, another Anti-Bourgeois Liberalization Campaign would be sure to follow, on an even larger scale than before. Conservatives would make a comeback and reform programs would come to a standstill or even be reversed. Chinese history would go through another period of zigzagging. The two approaches promised to result in two totally different outcomes.<br />
<br />
&nbsp; &nbsp; However, the crux of the issue was Deng Xiaoping himself. I hoped at the time that he could just relax things a little bit, for example, by saying something like &quot;It seems that when Li Peng gave his report on April 25, we overreacted to the situation. It now appears that the student demonstrations are not such an overwhelming problem.&quot; With something like this to work with, I could turn the situation around without even putting any of the liabilty on Deng. The Politburo Standing Committee and I could take responsibilty.<br />
<br />
&nbsp; &nbsp; However, if Deng refused to relax his position, then there was no way for me to change the attitudes of the two hard-liners, Li Peng and Yao Yilin. If they did not change their view, it would be difficult for the Standing Committee to carry out the principles of reducing tensions and opening dialogue. I was very well aware that Deng had always taken a tough stance on these kinds of issues. In addition, he had been prejudiced by Li Peng's reports, so it would be extremely hard for me to make him change his position.<br />
<br />
&nbsp; &nbsp; I was eager to have a talk with Deng and to gain his support. I phoned (Deng's secretary) Wang Ruiln asking for a meeting with Deng, but Wang said Deng had not been feeling well lately and he worried that his health problems might make him unable to receive Gorbachev, which would be a serious matter indeed. So he asked that I not report anything to him at that time. To this day, I stil believe that what Wang said was the truth;<br />
Deng was indeed in bad health then. <br />
 <br />
&nbsp; &nbsp; On May 2, I explained my idea to Yan Mingfu and asked him to contact Deng via Yang Shangkun and others who were closer to Deng. <br />
 <br />
&nbsp; &nbsp; On May 3, I went to visit Yang Shangkun at his home. Yang said that he had already spoken with Wang Ruiln and Deng's children, and they believed it would be difficult to reverse the position taken in the April 26 editorial, but thought it could be downplayed by not mentioning it again while gradually turning away from it. They said that if I were to talk to Deng then, only to have him reaffirm his stand, it would make it even more difficult to turn things around in the future. <br />
 <br />
&nbsp; &nbsp; Yang said, &quot;Those of you who are in the front lines can turn things around gradually.&quot; Yang Shangkun also indicated that he could appeal to the other members of the Standing Committee. That same day, Yan Mingfu came by my home and told me that Wang Ruiln and Deng's children said that those in charge of the Central Committee should deal with the student movement as they saw fit, according to the situation. If we talked to Deng then, only to have him disagree, then we would only have made matters worse.<br />
 <br />
&nbsp; &nbsp; In the days that followed, things progressed according to this idea of downplaying and gradually changing. My May Fourth speech was also based on this idea: the tone was distinctly different from the April 26 editorial, yet I used no phrasing that directly contradicted it.<br />
<br />
&nbsp; &nbsp; After the May Fourth speech, Yang Shangkun told me the result of his discussions with other members of the Politburo Standing Committee: Hu Qili and Qiao Shi agreed with the new approach; Li Peng and Yao Yilin opposed it. Comrade Wan Li, whom I spoke to directly, was in complete agreement with the new approach. This would mean that among the Standing Committee members and those who had attended the Standing Committee meeting, a majority supported me. <br />
 <br />
&nbsp; &nbsp; Yang also told me that he had spoken with (influential Part elder) Peng Zhen, who was entirely supportive of my position. Peng told him that if Deng were later to look for someone to place the blame on, &quot;Ziyang should not be left alone to bear the responsibilty,&quot; that he and Yang should also share responsibilty. This was his way of expressing his determination to support me.<br />
<br />
&nbsp; &nbsp; Before my return, when the Beijing Party Committee had proposed imposing martial law, Yang Shangkun had responded with sharp criticism: &quot;How could we justify to the rest of the world imposing martial law on our capital?&quot; I believe that Yang Shangkun held a moderate view toward the student demonstrations before Deng decided to impose martial law.<br />
<br />
&nbsp; &nbsp; On May 4, I delivered a speech to delegates of the Asian Development Bank regarding the student demonstrations. The speech was drafted by Bao Tong in accordance with my views. <br />
 <br />
&nbsp; &nbsp; In this speech, I conveyed the need to resolve the matter in a cool, reasonable, restrained, and orderly manner based on the principles of democracy and law. I also pointed out that the student demonstrators had expresséd both approval of and dissatisfaction with the Part and the government, and that they were absolutely not against the basic foundations of our system. Instead they were merely asking us to correct some of our flaws. I also said that in demonstrations of this magnitude, one could not rule out the fact that some people might want to manipulate things<br />
according to their own interests, but that this would not result in a major upheaval in China.<br />
<br />
&nbsp; &nbsp; After that speech, positive responses were received from a wide range of sources, both domestic and overseas.<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; After May 5 and in the days to follow, many universities in Beijing resumed classes. The director of the Xinhua News Agency in Hong Kong, Xu Jiatun, who was then in Beijing, sent me a handwrtten note, in which he mentioned that when he had met with Yang Shangkun on May 4, Yang<br />
had expressed total agreement with my speech.<br />
 <br />
&nbsp; &nbsp; At this time of widespread support, Li Peng came to my house on the evening of May 4 and was forced to commend me for my speech. He said he would follow up with some of the issues I'd mentioned when he himself met with delegates of the Asian Development Bank. But when I pointed out that the April 26 editorial was problematic, he disagreed.<br />
<br />
&nbsp; &nbsp; Because I could not meet with Deng himself, I discussed the matter with other comrades as mentioned above and attempted to turn the situation around gradually. Indeed, the situation was gradually turning around. When this approach was being taken, the situation became calmer and most of the students returned to their classes. However, they were waiting to see what happened next; that is, how the promises made in the May Fourth speech would be realized.<br />
<br />
&nbsp; &nbsp; I thought it best to use the time of relative calm to take active measures to set up dialogue with students and all other social groups, to respond to the issues of deepest concern to the students, and to adopt some of the students' reasonable ideas. These would have been concrete steps in the direction of opening dialogue and reducing tensions. <br />
 <br />
&nbsp; &nbsp; While I and other members of the Politburo Standing Committee and those who had attended Standing Committee meetings were actively attempting to effect this turnaround, Li Peng and others in his group actively attempted to block, delay, and even sabotage the process, so that the proposed dialogue and methods to reduce tensions laid out in the May Fourth speech could not be carried out. <br />
<br />
&nbsp; &nbsp; Meanwhile, on the topics of most concern to people and raised by the students-such as corruption, government transparency, democracy, rule of law, and public scrutiny of government-we needed to take active measures. I suggested establishing a Commission Against Corrption with real authority, under the National People's Congress (NPC) , that would independently accept reports and conduct investigations into the unlawful activities of familes of senior Part leaders; strengthening the public's abilty to scrutinize the government; increasing government transparency and speeding up the process of establishing laws on the press and demonstrations; and adopting the practice common around the world of protecting<br />
the people's democratic rights by establishing specific laws.<br />
<br />
&nbsp; &nbsp; I further proposed callng a meeting of the NPC Standing Committee to conduct public hearings on the auditing of several major corporations that were commonly believed to be plagued by corruption. All the arrangements and further investigations should be managed by the NPC, because in the minds of many people, the NPC was more transparent than the Party or the government.<br />
 <br />
&nbsp; &nbsp; My general approach was thus to carry out reform in the areas of concern to the people, so that we could reduce the level of dissatisfaction among the people and the students, so as to reduce and end the student demonstrations, and at the same time we could seize the opportunity to boost political reform. Tackling these specific issues would enable the NPC to play its rightful role as the highest authority in the nation while directing the students' attention toward furthering political reform.<br />
<br />
&nbsp; &nbsp; On May 13, when Yang Shangkun and I went to Oeng Xiaoping's residence to discuss issues pertaining to Gorbachev's forthcoming visit, I also talked to him about the recent situation with the student demonstrations. I expressed my views about open dialogue, tackling corruption, and transparency. In principle he agreed, and said that there was &quot;a need to take the opportunity to tackle corruption, to make a concerted effort.&quot; He also mentioned that there was a need for increased transparency. <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; There had been many rumors circulating about the sons and daughters of senior leaders doing business by taking advantage of official government resources. Many of these rumors accused my own sons and daughters. Because of this, on the afternoon of May 1, I proposed at a Politburo Standing Committee meeting that the Politburo order the Central Discipline Correction Commission and the Ministry of Supervision to open an investigation of my family members. Later I sent a formal letter to the Politburo to request that it support my proposal.<br />
<br />
&nbsp; &nbsp; Another issue that the students cared about was press freedom. On May 6, in a discussion about reforming press policy with comrades (PSC member) Hu Qili and (Central Committee Secretariat member) Rui Xingwen, I proposed that attention be paid when drafting new press laws to relaxing the restrictions on news reporting, editorials, and commentary. <br />
 <br />
&nbsp; &nbsp; On May 3, I went to (NPC Standing Committee chairman) Wan Li's home and talked with him about the student demonstrations. I commented that some of the leadership had overreacted to the student demonstrations, a result mainly of an outdated mentality formed by the prolonged focus on class struggle. Times had changed, and we needed to change this mentality to coincide with the trend of democracy and rule of law. He completely agreed with me, and said that many leaders from Tianjin and Beijing had complained to him that the Central Committee had been too soft on the student demonstrations, another example of this kind of old mentality at work. He also suggested that these problems needed to be resolved. <br />
 <br />
&nbsp; &nbsp; Either during the Politburo Standing Committee meeting of May 8 or the Politburo meeting of May 10 (I don't remember which), he (Wan Li), made some very good suggestions about following the worldwide trend toward democracy and properly addressing the issues that the students had taken up in their demonstrations. He expressed his full support of my Politburo proposal when he held the NPC Standing Committee meeting. He also set a date for another meeting of the NPC Standing Committee, to be held soon, and listed these issues on the meeting's agenda.<br />
<br />
&nbsp; &nbsp; On May 9, Wan Li came to my house to tell me he was about to leave for an official visit to Canada and the United States. He had thought about speaking to Deng Xiaoping about the issue before leaving, but had not found the time to do so. On several occasions while in Canada and the United States, he called the student movement both patriotic and democratic, praising it highly.<br />
<br />
&nbsp; &nbsp; The attitude that Wan Li adopted toward the student demonstrations was no accident. He had always believed in opening up to democracy and had always supported political reform. He had been opposed to the Anti-Liberalization Campaign of 1987 and had given speeches specifically about the democratization of decision making. Among senior leaders of the Central Committee, he was the one who most ardently supported reform.<br />
<br />
&nbsp; &nbsp; Li Peng, Yao Yilin, and Beijing Municipality (Part Secretary) Li Ximing made fierce attempts to block, resist, and delay the carring out of my proposals. They did not openly express opposition to my May Fourth speech in the few days following, and even voiced a few words of praise. But in factthey were working furiously to distort it. They claimed that my speech was actually in line with their April 26 editorial but had just taken a slightly different angle. They then asked (State Education Commission Vice Minister) He Dongchang to spread a notion at a meeting held by the State Council with several university Part chiefs that Zhao's speech represented only his personal opinion and did not represent the Central Committee's. This message was quickly spread among the students. <br />
 <br />
&nbsp; &nbsp; They attempted even more furiously to resist and to delay any dialogue with the students. Originally the idea of the dialogue was to meet directly with the student demonstrators, but they not only denied the participation of any student organizations that had emerged during the demonstrations, they also prohibited the students from selècting their own representatives. They insisted on letting only students from official student organizations partcipate, which could not in any way have been representative of the student demonstrators. Wasn't conducting dialogue in this manner the same as completely rejecting dialoguè altogether? Also, when they did hold dialogues, they did not discuss things openly or seek diverse opinions with an attitude of sincerity. Instead they were merely paying lip service, in the same way that they had always handled foreign reporters at press conferences, presenting an image that would benefit themselves politically. This left the students with the impression that the government's offer to hold dialogues with them was totally insincere.<br />
<br />
&nbsp; &nbsp; I repeatedly criticized this behavior, but was ignored. On efforts to fight corruption and increase transparency, they were even more remiss. Li Peng even opposed listing these issues on the agenda of the NPC Standing Committee meeting. He called me specifically to object to putting these items on the agenda. <br />
 <br />
&nbsp; &nbsp; Because of that, after students had returned to their classes and several days had passed, they could not see how the government was taking any real actions. The .dialogues that took place seemed aimed only at brushing them off, and of course no concrete steps were taken on reform; so in fact, doubts grew about my May Fourth speech. A more intense confrontation was therefore made inevitable.<br />
<br />
&nbsp; &nbsp; Now we must answer the question &quot;Why did the student movement continue for such a long time?&quot;<br />
<br />
&nbsp; &nbsp; They claim that my May Fourth speech had revealed a rift within the Central Committee, into the so-called &quot;two voices.&quot; That is not true! The real reason was that the guidelines laid out after my return from North Korea-namely to defuse tensions, to open dialogue, to resolve the issue through democracy and law, and to start tackling hot issues by proceeding with political reform-had been blocked, resisted, and sabotaged by Li Peng and his associates.<br />
<br />
&nbsp; &nbsp; Just before Gorbachev's arrval, Li Peng said to me, &quot;You're not going to continue to use soft measures to deal with the student demonstrations, are you? After so much time has elapsed, haven't they already been proven useless?&quot;<br />
<br />
 <br />
&nbsp; &nbsp; This comment fully revealed his hidden ill intentions. He used resistance and sabotage to ensure that efforts to resolve the student demonstrations on the basis of democracy and law would fail, with the intention of looking for an excuse to crush the student demonstrations using violent means.<br />
<br />
&nbsp; &nbsp; An incident at the World Economic Herald in Shanghai also happened during my visit to North Korea. It started when the newspaper printed a report on the activities commemorating Hu Yaobang. The Shanghai Part Committee found the content inappropriate and ordered the paper to cut the report, but it refused to do so. The Shanghai Part Committee therefore decided to close down the paper for reorganization and suspended the chief editor, Qin Benli.<br />
 <br />
According to what I heard, (Shanghai Part chief) Jiang Zemin had phoned the office of Deng Xiaoping for a directive on handling this. The students and the masses were in a highly charged emotional state then. By doing what it did, it (the Shanghai Part Committee) not only angered the Herald's staff, but also provoked general opposition. from the staff at other news agencies in Beijing, Shanghai, and other places around the country. Many (members of these staffs) took to the streets to voice their support of the Herald and demanded that the Shanghai Part Committee reverse its decision against the Herald. Their actions coincided with the student demonstrations and the two groups mutually reinforced one another.<br />
<br />
When I'd returned to the country, I felt the Shanghai Part Committee had been too rigid and simplistic in dealing with the issue and had also chosen a bad time to do it. But since the matter had already passed, I didn't comment on it; it seemed inappropriate for me to admonish the Shanghai Party Committee and side with the staff of the news organization. The Central Committee therefore took the position of not intervening, allowing the Shanghai Part Committee to resolve the issue itself. <br />
 <br />
On May 2, when I was holding a talk with members of other political parties, Yan Mingfu reported that someone representing the local Shanghai United Front Work Department had told him that the Shanghai Party Committee wanted to back down from its previous position. They hoped the Central United Front Work Department would assist them. I replied, &quot;Since the Shanghai Party Committee has made such a request, you should help them find a solution.&quot;<br />
<br />
On May 10, Jiang Zemin came to Beijing and talked to me about plans to reduce tensions. I told him the matter should be resolved in Shanghai without the interference of the Central Committee, thereby avoiding creating suspicion that the Shanghai government was merely bowing to pressure from the Central Committee. Jiang Zemin was unhappy about this, and after June Fourth, listed this incident as one of the accusations against me.</div>

							
							
							
							
