													<h2>4: The Crackdown</h2>
						
						
																											<div id="postmessage_105335" class="t_msgfont">Zhao's final attempts to soften the government's response to the protests fall short, as Deng lines up in support of Premier Li Peng's tough stance. At a tense meeting at Deng's home, which Zhao describes in some detail, the paramount leader authorizes the imposition of martial law. Zhao is opposed and refuses to carry out the policy; he is soon excluded from decision making. Zhao visits Tiananmen Square to urge the students to return to campus, but it's too late. He learns of the June Fourth crackdown when he hears gunshots from his home.<br />
<br />
&nbsp; &nbsp; Having grown entirely disillusioned with the government dialogues, the students decided to use the occasion of Gorbachev's visit to stage large-scale street demonstrations and a hunger strike. They believed it was the best opportunity to exert pressure on the government, which would be compelled to show tolerance during the state visit. But the students were mistaken, for the more they pushed ahead, the more pretext Li Peng and his associates had to crack down on them using violent means.<br />
<br />
&nbsp; &nbsp; When I got wind of this, I took the opportunity to deliver a speech on May 13 at a gathering of workers. Roughly, what I told them was that it would be unreasonable for the students to disturb international state talks and do damage to the Sino-Soviet Summit because their demands had not been satisfied. Moreover, it would not gain the support of most people. I hoped they would take the big picture into consideration, and not injure our friends while delighting our enemies.<br />
<br />
&nbsp; &nbsp; My plea was printed in all the major papers. However, the students did not respond to it at all; they proceeded regardless. On the afternoon of May 13, more than two hundred students from more than twenty universities, with more than a thousand others to act as guards, entered Tiananmen Square to stage a sit-in and a hunger strike. From this day on,<br />
the students occupied the square, up until the bloody incident of June Fourth.<br />
<br />
&nbsp; &nbsp; The student hunger strike received widespread sympathy and support. Tens of thousands of people from various government departments and other organizations as well as ordinary urban residents staged demonstrations in support. The numbers grew from day to day. The number of hunger strike participants also increased, reaching between two and three thousand people at its peak. Students had become enthralled by the situation, making it even harder to persuade them to leave.<br />
<br />
&nbsp; &nbsp; At the time, the students' actions were still mostly spontaneous. Even though they had formed a command center, not one leader among them could make a coolheaded decision. Even when a decision was made, it was not authoritative in any way. Leaders were changed frequently at the command center, and things proceeded according to the ideas of whoever's<br />
voice was loudest and most rousing. We tried to persuade the student leaders by mobilizing university leaders and professors to talk to them, but these efforts fell on deaf ears. Because of Li Peng and his associates, the principal guidelines of reducing tension, opening dialogue, and persuasion had not been implemented.<br />
<br />
&nbsp; &nbsp; On the fourth day of the hunger strike, some of the students began fainting. I was extremely worried that if this continued, some students might die. We would have a hard time answering to our people.<br />
<br />
&nbsp; &nbsp; On the night of May 16, after meeting with Gorbachev, I called a Politburo Standing Committee meeting to discuss issuing a public statement in the name of its five members to urge the students to stop their hunger strike. The draft contained the sentence &quot;The passionate patriotism of the students is admirable, and the Central Committee and the State Council<br />
approve of their deeds.&quot;<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; Li Peng opposed it, saying, &quot;Mentioning 'admirable' is quite enough. Do we have to also add that we 'approve'?&quot; Yang Shangkun replied, &quot;The students propose action against corrption. We can say we approve of this.&quot;<br />
<br />
&nbsp; &nbsp; I was quite repelled by Li Peng's attitude, and said, &quot;If we don't mention 'approval: it's as if we'd said nothing at all. Then what's the purpose of issuing a statement? Our current task is to issue a statement that will calm the students' emotions. We must not now quibble over the wording.&quot; A majority of the Standing Committee members agreed to include this<br />
line, so it was narrowly passed.<br />
 <br />
&nbsp; &nbsp; However, by this time I believed that the situation had progressed to a tage where even this statement would not end the hunger strike, since the strongest demand was a reversal of the April 26 editorial's characterization of the demostrations. I felt that this was a problem that we could no longer bypass. If this key issue was not resolved, there would be no way to end the hunger strike and proceed with dialogue. If the hunger strike continued, then unpredictable but extremely grave consequences would follow.<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; So for the first time, I formally proposed revising the judgment of the April 26 editorial in a Politburo Standing Committee meeting. Li Peng immediately opposed this.<br />
<br />
&nbsp; &nbsp; He said the designation contained in the April 26 editorial was drafted strictly according to Deng Xiaoping's own words and therefore could not be changed. My rebuttal was that the editorial had been drafted according to the minutes fo the April 24 Politburo Standing Committee meeting andthat Deng Had merely voiced support of the discussion that came out of that meeting.<br />
<br />
&nbsp; &nbsp; Yang Shangkun warned that revising the April 26 editorial would damage Deng Xiaoping's image. I replied that we could arrange matters in such a way as to aviod causing any damage to Deng's reputation by having the Politburo Standing Committee take collective responsibility. I also said that since I had sent the telegram from North Korea agreeing with Deng's decision, I should take responsibility for the April 26 editorial. If necessary, it could be added that I had approved it.<br />
<br />
&nbsp; &nbsp; Li Peng said abruptly, &quot;This is not the proper attitude of a politician!&quot; The result was that a revision of the April 26 editorial was unable to proceed.<br />
<br />
&nbsp; &nbsp; I had no other choice but to express my views to Deng personally, in a face-to-face meeting. On the 17th, I phoned to request to see Deng. Later, a member of Deng's staff asked me to go to Deng Xiaoping's home in the afternoon for a meeting.<br />
<br />
&nbsp; &nbsp; All the members of the Politburo Standing Committee plus (Yang) Shangkun were already there. At the time, Wan Li, who would have attended, was still abroad. Since I had asked for a personal meeting with Deng, only to have Deng call for a full Standing Committee meeting at his home, I realized that things had already taken a bad turn.<br />
<br />
&nbsp; &nbsp; First, I expressed my views, roughly as follows:<br />
<br />
&nbsp; &nbsp; The situation with the student demonstrations has worsened, and has grown extremely grave. Students, teachers, journalists, scholars, and even some government staff have taken to the streets in protest. Today, there were approximately 300,000 to 400,000 people. Quite a large number of workers and peasants are also sympathetic. Besides the hot issues of corruption and government transparency, the main impetus for all these different social groups is that they want an explanation for how the Party and the government can be so coldhearted in the face of hungerstriking students, doing nothing to try to save them. The key issue blocking dialogue with the students is the judgment passed by the April 26 editorial. The editorial, which caused so much misunderstanding, must have been unclear or incorrectly expressed in some way. The only way to bring about some kind of resolution would be to somewhat relax the judgment from this editorial. This is the key and, if adopted, will gain wide social support. If we remove the labeling of the student movement, we will regain control over the situation. If the hunger strike continues and some people die, it will be like gasoline poured over a flame. If we take a confrontational stance with the masses, a dangerous situation could ensue in which we lose complete control.<br />
<br />
<br />
&nbsp; &nbsp; While I was expressing my views, Deng appeared very impatient and displeased. As soon as I had finished speaking, Li Peng and Yao Yilin immediately stood up to criticize me.<br />
They placed blame for the escalation of the demonstrations entirely on the May Fourth speech I presented to the Asian Development Bank. That was the first time I heard them voice criticisms of my ADB speech. Though they had opposed it in actuality, they had never said so openly before. The intensity of their accusations caught me completely by surprise. From the unrestrained way in which these two attacked me, I could see that they had already gained Deng Xiaoping's tacit approval.<br />
<br />
&nbsp; &nbsp; Hu Qili expressed his view that the editorial should be revised. Qiao Shi equivocated. (Yang) Shangkun opposed revising the editorial, thereby having a very bad impact on the situation. He said, &quot;Liao Hansheng believes that martial law should be imposed. Perhaps we should consider imposing martial law...&quot; Previously, Shangkun had always opposed martial law, but this time he quoted (veteran military leader) Liao Hansheng, when in fact he himself had changed his position.<br />
<br />
&nbsp; &nbsp; In the end, Deng Xiaoping made the final decision. He said, &quot;The development of the situation has only confirmed that the judgment of the April 26 editorial was correct. The reason that the student demonstrations have not subsided is something within the Party, and that is Zhao's May Fourth speech at the ADB meeting. Since there is no way to back down now without the situation spiraling completely out of control, the decision is to move troops into Beijing to impose martial law.&quot;<br />
<br />
&nbsp; &nbsp; He also appointed Li Peng, Yang Shangkun, and Qiao Shi as a threeperson team to implement the imposition of martial law. When Deng was finished, I said that having a decision was always better than not having one, but I was extremely worried about the grave consequences this would have. As General Secretary, it would be difficult for me to manage and effectively carry out this decision. Deng said, &quot;If this turns out to be a wrong decision, we wil all be responsible.&quot;<br />
<br />
&nbsp; &nbsp; During this meeting, Li Peng also claimed that contents of Politburo Standing Committee meetings had been leaked to the public, and that there were some bad elements on the inside, (Political Secretary of the PSC) Bao Tong being one of them. I replied, &quot;You must be responsible when making such claims! What evidence do you have?&quot; He said, &quot;I do<br />
have evidence that I wil reveal to you later.&quot;<br />
<br />
&nbsp; &nbsp; I walked out as soon as the meeting adjourned. If Deng asked the others to remain or discussed other matters, I never knew.<br />
<br />
&nbsp; &nbsp; At that moment, I was extremely upset. I told myself that no matter what, I refused to become the General Secretary who mobilzed the miltary to crack down on students. Upon returning home, under heightened emotions, I called on Bao Tong to draft a letter of resignation for me to send to the Standing Committee.<br />
<br />
&nbsp; &nbsp; At that evening's meeting to brief the Standing Committee, I refused to accept the assignment to chair the meeting of cadres to announce martial law. I said, &quot;It seems my mission in history has already ended.&quot; Yang Shangkun replied to me, &quot;This kind of issue cannot be raised now. No changes in leadership should be made.&quot; He meant that my position as General Secretary should not be changed.<br />
<br />
&nbsp; &nbsp; As soon as my letter of resignation reached the Service Bureau of the Central Committee General Office, Shangkun found out about it. He phoned me and repeatedly beseeched me to revoke my decision. Shangkun said, &quot;If this information leaks out, then the situation will be even worse. We should not pour gasoline on a flame.&quot;<br />
<br />
&nbsp; &nbsp; I conceded his argument and on May 18 notified the General Office to halt the distribution of the letter. My secretary (Li Shuqiao) later retrieved it.<br />
<br />
&nbsp; &nbsp; Here I would like to clarify something about this meeting called by Deng that resolved to impose martial law and crack down on the students. There has been public hearsay that the Politburo Standing Committee meeting resulted in a vote of three against two, but in fact there was no &quot;three versus two&quot; vote. There were only a few people in attendance. Among the members of the Standing Committee, it was two against two: Hu Qili and I were for revising the editorial, Yao Yilin and Li Peng were ardently opposed, and Qlao Shi remained neutral by not expressing any clear view.<br />
<br />
&nbsp; &nbsp; There was no such thing as a &quot;three versus two&quot; vote. Of course, if the opinions of Deng and Yang, who were not members of the Standing Committee, were added, in the overall count of all the people who attended that meeting, they were certainly a majority. However, in fact, the Standing Committee held no formal vote.<br />
<br />
&nbsp; &nbsp; During those few days, many prominent people and senior Party comrades phoned or wrote letters to me and to the Central Committee, appealing to us to treat the students properly, to acknowledge that the students' actions had been patriotic, and to change the wrongful stance assumed toward the students. Among them were those whom Deng Xiaoping had always held in high regard, such as senior comrades like Li Yimang.<br />
<br />
&nbsp; &nbsp; On May 18, I forwarded a selection of these letters to Deng and wrote to him to reiterate my position, hoping he would reconsider. Though I knew there was very little hope of this, I had to make one last attempt. The original text of my letter is as follows:<br />
<br />
&nbsp; &nbsp; Comrade Xiaoping,<br />
<br />
&nbsp; &nbsp; I have forwarded several appeals from influential senior comrades. I hope you will read them.<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;The current situation is extremely grave, the most urgent matter of which is to stop the student hunger strike (for which people feel great sympathy) so as to avoid any deaths. The crucial request that must be granted in order to stop the hunger strike is the reversal of the labeling and judgment made of them in the April 26 editorial, and acknowledgment of their actions as patriotic.<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;I have considered this carefully, and feel we must, however painful, resolve to make this concession. So long as our key leaders personally go out among the masses and admit this, the intensity of emotions will be greatly reduced, and then other matters can be resolved. Even if you must eventually take some resolute measures to maintain-order, we must take this step first. Otherwise, imposing harsh measures while a majority of people are adamantly opposed may result in serious repercussions that threaten the fate of the Part and the state.<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;With profound concern, I again appeal to you to consider this suggestion.<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; ZhaoZiyang<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; May 18<br />
<br />
<br />
&nbsp; &nbsp; This was the first letter that I sent to him after the May 17 meeting at his house that decided upon the imposition of martial law. As I'd expected, there was no reply.<br />
<br />
&nbsp; &nbsp; On the evening of May 17, the Central Committee General Office made arrangements for leaders of the Central Committee to visit the hunger-striking students who were in the hospital. Li Peng initially said he would not go, but just as the van started up to leave, he showed up. It turns out that he had changed his mind after hearing that I was going.<br />
<br />
&nbsp; &nbsp; The same thing happened on the early morning of May 19 when I went to visit the students in Tiananmen Square. He opposed my going and urged the General Office to stop me. I felt that with so many students on hunger strike for as many as seven days, it had become indefensible that none of the leaders of the Central Committee had paid a visit. I insisted<br />
on going, saying that if no others went, I would go alone. Once he saw that I was intent upon going and could not be deterred, he changed his mind. But he was terrified and fled very soon after we arrived at the square.<br />
<br />
&nbsp; &nbsp; Besides greeting the students, I improvised a speech that ended up being printed in all the major newspapers in the capital. When I spoke, I was merely trying to persuade them to end the hunger strike, telling them they were still young and must treasure their lives. I knew all too well that though their actions had won widespread sympathy both across the country and abroad, it was of no use against the group of elders who had taken a hard-line position. It would not matter if the hunger strike continued or if some people died; they (the elders) would not be moved. I felt it was a waste for these young students to end their lives like this.<br />
<br />
&nbsp; &nbsp; However, the students did not understand what I meant. Even less could they imagine the treatment in store for them. Of course, I was later the target of harsh criticisms and accusations for this speech to the students.<br />
<br />
&nbsp; &nbsp; After the meeting at Deng's home on May 17, Li Peng and his associates acted abnormally in many ways. Whether I was going to the hospital or to the square to visit students, he repeatedly attempted to block me. When I arrived and I exited the van, he rushed out in front of me, which was contrary to custom. Someone later told me that he instructed people to hint to the cameramen not to include images of me, because it would become &quot;inconvenient&quot; in the case of future leadership changes.<br />
<br />
&nbsp; &nbsp; From the evening of May 17 to May 19, none of the issues regarding martial law were imparted to me. I only learned about Li Peng's dialogue with the students on the 19th from seeing it on television.<br />
<br />
&nbsp; &nbsp; On the afternoon of the 19th, however, I was suddenly delivered a notice for the meeting that would announce the imposing of martial law and given the text of his (Li Peng's) speech, and was asked to chair and speak at the meeting. Yet I was not notified about how this meeting was to proceed, where it would be held, who would attend, or what other items were to be on the agenda.<br />
<br />
&nbsp; &nbsp; His speech even included the statement, &quot;The student demonstrations escalated after May Fourth.&quot; Later, they must have felt that the statement too blatantly placed the blame on my May Fourth speech, so when it was published in the newspapers, it was changed to &quot;The student demonstrations escalated in early May.&quot; This was an open implication that my May Fourth speech had caused the escalation of demonstrations. Li Peng also announced to members of the State Council that I had made a big mistake. They also held an exclusive meeting prior to the larger meeting to announce martial law.<br />
<br />
&nbsp; &nbsp; All of this added to my realization that I had been excluded from decision making. To this day, I still don't know when that decision was made. On the 17th at Deng's place, when deciding to impose martial law, even though Li Peng, Yang Shangkun, and Qiao Shi were appointed to conduct the affair, Deng also noted that &quot;Zhao is still the General Secretary.&quot; But<br />
in fact, in the several days that followed, I was entirely pushed aside.<br />
<br />
&nbsp; &nbsp; On the 19th, I applied for a three-day leave from the Politburo. I suggested that Li Peng chair the Politburo Standing Committee and refused to attend the mobilization meeting to announce martial law.<br />
<br />
&nbsp; &nbsp; At the time, the number of demonstrators supporting the hunger strike in Tiananmen Square had become much smaller. The hunger strike was abandoned and turned into a sit-in. Many of the Beijing university students had already returned to their schools. Those who remained in the square were mostly students from other cities.<br />
<br />
&nbsp; &nbsp; The announcement of martial law on May 19 (actually May 20) was another stimulant, once again mobilizing the masses. Participants of the sit-in increased and supporters from other social groups crowded the streets. Beijing residents were particularly aggrieved by the decision to call troops to Beijing to execute martial law. Troops that received their orders were blocked along their way, everywhere. Groups of old ladies and children slept on the roads. Troops were stopped in the suburbs of Beijing, unable to enter the city. The standoff lasted more than ten days.<br />
<br />
&nbsp; &nbsp; On May 21, Qiao Shi came to my house to discuss the situation. He said, &quot;Quite a number of people are feeling like they are 'riding a tiger, unable to get off: If it were not for Deng's insistence and his decision to call more troops to Beijing, a great tragedy might be avoided. But now the troops have been blocked from entering, martial law is ineffective, and milions of students, residents, workers, and cadres from government organizations are out on the streets or gathered on Tiananmen Square. If this continues, the capital is in danger of becoming paralyzed.&quot;<br />
<br />
&nbsp; &nbsp; At that moment, I thought that perhaps if we held the National People's Congress Standing Committee meeting ahead of schedule we could allow the NPC, the organization with proper authority, to use the means of democracy and law to turn the situation around. On May 21, I spoke to (Central Committee Secretariat member) Yan Mingfu about this idea, and asked him to speak with (Yang) Shangkun to see if it was feasible.<br />
<br />
&nbsp; &nbsp; Before this, (NPC vice chairman) Peng Chong had come over to talk. He said that since Wan Li was abroad, he (Peng Chong) had held a meeting with the heads of the NPC committee. They all felt that an NPC Standing Committee meeting should be held. He also went to Yuquanshan (Jade Spring Mountain, west of Beijing) to visit (influential Party elder) Peng Zhen, who also agreed that this should be done. They had already written a report to the Central Committee requesting that Wan Li return from abroad ahead of schedule.<br />
<br />
&nbsp; &nbsp; In the afternoon of the 21st, (PSC member) Hu Qili came to my house to report that no one had responded to the request to have Wan Li return. It was in limbo. I asked Hu Qili to tell Peng Chong to telegram Wan Li directly in the name of the Part Group of the NPC to request his return. Hu Qili asked if he could say that I had agreed to this, and I said, &quot;Yes.&quot;<br />
<br />
&nbsp; &nbsp; I then phoned (Vice Premier) Wu Xueqian and asked him to find a way to send the telegram. I later learned that Li Peng sent another telegram to Wan Li to tell him not to return. It is possible that he had Deng's prior approval, so Wan Li was unable to make an early return.<br />
<br />
&nbsp; &nbsp; On the night of June 3rd, while sitting in the courtard with my family, I heard intense gunfire. A tragedy to shock the world had not been averted, and was happening after all.<br />
<br />
&nbsp; &nbsp; I prepared the above written material three years after the June Fourth tragedy. Many years have now passed since this tragedy. Of the activists involved in this incident, except for the few who escaped abroad, most were arrested, sentenced, and repeatedly interrogated. The truth must have been determined by now. Certainly the following three questions should have been answered by now.<br />
<br />
&nbsp; &nbsp; First, it was determined then that the student movement was &quot;a planned conspiracy&quot; of anti-Part, anti-socialist elements with leadership. So now we must ask, who were these leaders? What was the plan? What was the conspiracy? What evidence exists to support this? It was also said that there were &quot;black hands&quot; within the Part. Then who were they?<br />
<br />
&nbsp; &nbsp; Second, it was said that this event was aimed at overthrowing the People's Republic and the Communist Party. Where is the evidence? I had said at the time that most people were only asking us to correct our flaws, not attempting to overthrow our political system.<br />
<br />
&nbsp; &nbsp; After so many years, what evidence has been obtained through the interrogations? Have I been proven right, or have they?<br />
<br />
&nbsp; &nbsp; Many of the democracy activists in exile say that before June Fourth, they had still believed that the Part could improve itself. After June Fourth, however, they saw the Part as hopeless and only then did they take a stand to oppose the Party. During the demonstrations, students raised many slogans and demands, but the problem of inflation was conspicuously<br />
missing, though inflation was a hot topic that could easily have resonated with and ignited all of society. If the students had intended on opposing the Communist Part back then, why hadn't they utilized this sensitive topic? If intent on mobilizing the masses, wouldn't it have been easier to raise questions like this one? In hindsight, it's obvious that the reason the students did not raise the issue of inflation was that they knew that this issue was related to the reform program, and if pointedly raised to mobilize the masses, it could have turned out to obstruct the reform process.<br />
<br />
&nbsp; &nbsp; Third, can it be proven that the June Fourth movement was &quot;counterrevolutionary turmoil,&quot; as it was designated? The students were orderly. Many reports indicate that on the occasions when the People's Liberation Army came under attack, in many incidents it was the students who had come to its defense. Large numbers of city residents blocked the PLA from entering the city. Why? Were they intent on overthrowing the republic?<br />
<br />
&nbsp; &nbsp; Of course, whenever there are large numbers of people involved, there will always be some tiny minority within the crowd who might want to attack the PLA. It was a chaotic situation. It is perfectly possible that some hooligans took advantage of the situation to make trouble, but how can these actions be attributed to the majority of the citizens and students? By now, the answer to this question should be clear.</div>

							
							
							
							
