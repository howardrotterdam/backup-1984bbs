													<h2>7: Zhao's Talk with Gorbachev</h2>
						
						
																											<div id="postmessage_105621" class="t_msgfont">One of the mysteries about the events leading up to the Tiananmen Massacre is when precisely Deng Xiaoping decided to part ways with his reform ally, Zhao Ziyang. When Zhao met with Gorbachev, he stressed that Deng, despite a lack of official titles, was still in charge. While Zhao says he meant only to highlight the importance of Gorbachev's meeting with Deng, his detractors accused him of trying to subtly place blame for the political turmoil on Deng's shoulders. It's unclear whether Zhao's comment really cost him<br />
&nbsp; &nbsp; Deng's trust. But if it did, the losses of hundreds of lives could ultimately be traced to the paranoia and lack of judgment of one man, Deng, in a time of crisis. Zhao's intimate account sheds no light on the mystery, though it reveals his deep sense of regret for any misunderstanding, and his continued gratitude for his relationship with Deng.<br />
<br />
<br />
&nbsp; &nbsp; Here I'd like to comment on the issue of my talk with Gorbachev on May 16.<br />
<br />
&nbsp; &nbsp; Deng was quite displeased with my May Fourth speech at the Asian Development Bank conference. However, I'm afraid my talk with Gorbachev didn't just make him anger, but really hurt him. After June Fourth, he told (Nobel Prize-winning Chinese American physicist) Professor Tsung-Dao Lee that I had pushed him to the forefront during the student turmoil. What he actually meant was that I had abandoned him to confront the public alone. Notions of this kind circulated among the populace as well.<br />
<br />
&nbsp; &nbsp; When I talked with Gorbachev, I spoke of Deng Xiaoping's role in our country and in the Party. These comments were entirely intended to uphold Deng's prestige, but resulted in a great misunderstanding. People thought I was shirking responsibilities, pushing Deng to the forefront (and forcing him to) confront the public at a critical moment. I absolutely did<br />
not foresee this.<br />
<br />
&nbsp; &nbsp; Ever since the 13th Party Congress (in 1987) whenever I met with foreign leaders, especially fellow Part leaders, I always informed them that even though Deng was no longer on the Politburo Standing Committee, his role as the major decision maker in our Part had -not changed. This had almost become a convention. In April, I had informed Kim II Sung in North Korea of the same. What was different with this talk was that the message gained prominence through TV and newspaper coverage.<br />
<br />
&nbsp; &nbsp; Why did I do this?<br />
<br />
&nbsp; &nbsp; The publication of Deng's April 25 remarks by Li Peng and his associates had resulted in a public outcry. Students and youths were particularly unhappy with Deng. Because of the dissatisfaction with his remarks, they focused on and assailed his special position. I heard many remarks such as &quot;Why does the Politburo Standing Committee have to report to Deng Xiaoping, who is not even a member? This does not conform with the principles of the Part's organization!&quot; The phrase &quot;hanging a curtain to administer the affairs of state&quot; was spreading. Amid all of this, I thought I should come out with a clarification and an explanation.<br />
<br />
&nbsp; &nbsp; On May 13, two days before Gorbachev's arrival, I held a dialogue with delegates of workers and cadres from official workers' unions. A worker raised a question along these lines. I replied by explaining that this was in accordance with a resolution passed at the First Plenum of the 13th Central Committee. This plenum had decided that we must consult with Deng Xiaoping on any matters of great importance. This was for the benefit of the whole Part because Deng's political wisdom and experience was richer than that of any member of the Politburo Standing Committee. The answer-seemed to go over well, as that worker did not pursue the question any further. Hence, I thought that if we gave the same explanation through the press, it would have a positive effect on Deng's public image. At least it would clarify that this wasn't a case of Deng grabbing power, but rather a collective decision made at the Central Committee's First Plenum.<br />
<br />
&nbsp; &nbsp; Therefore, when I met with Gorbachev, I told him that our Party's First Plenum of the 13th Central Committee had formally decided that on major issues, we still needed Deng to be at the helm. Ever since the 13th Party Congress, we had always kept him informed and sought his opinion on major issues. Deng had always been fully supportive of our work and our collective decisions. In fact, the original resolution was not only that we should seek his opinion and keep him informed, but also that he could call for a meeting and make the final decision on major issues. Taking into consideration what the public would be able to accept, I intentionally did not mention this last point. I believed the public explanation that I did make would benefit Deng, and at the very least clarify that it was not an illegal situation, but in fact a legitimate one.<br />
<br />
&nbsp; &nbsp; There was another reason for me to make these remarks: Gorbachev's visit was a summit between China and the Soviet Union. Which person actually met with Gorbachev was of symbolic importance in defining such a summit. Of course, both domestically and abroad, everyone knew that the so-called &quot;Sino-Soviet Summit&quot; was between Gorbachev and Deng<br />
Xiaoping. But Gorbachev was the President of the U.S.S.R. and the General Secretary of the Communist Part, while Deng was neither President of the state nor General Secretary of the Party, but only the chairman of the Central Military Commission. My sincere intention was to prominently declare that the summit was defined by the meeting between Gorbachev and Deng, not between Gorbachev and anyone else.<br />
<br />
&nbsp; &nbsp; Originally, the Foreign Ministry planned to dilute the message, neither avoiding the issue altogether nor being too formal about it; it was not to be included in the declaration or in any formal discussions between the two parties. They asked me to say to Gorbachev, &quot;Our meeting as the General Secretaries of our respective parties naturally signifies the restoration<br />
of the relationship between our two parties.&quot; But on May 13, two days before I was to meet with Gorbachev, while I was talking to Deng at his home regarding Gorbachev's visit, Deng stated that the relationship between the two parties would be restored after he met with Gorbachev. This departed from the original plan of the Foreign Ministry. I paid specific attention to this remark from Deng.<br />
<br />
&nbsp; &nbsp; Because of all these considerations, after Gorbachev had already met with Deng, I started my meeting with him by saying that the relationship between our two parties had been restored by his meeting with Deng, that his meeting with Deng was the culmination of his visit. Naturally. I then followed up with discussion of Deng's position and the decision made by<br />
the First Plenum of the 13th Central Committee. My comment was meant to explain two issues simultaneously: why Gorbachev's meeting with Deng defined the summit and the fact that Deng's continued position as the paramount decision maker for the Chinese Communist Part was a ruling of the Central Committee, consequently legitimate. At the time, I felt that my remark was extremely appropriate, resolving problems in a natural way.<br />
<br />
&nbsp; &nbsp; After the talk, I initially received positive responses. Later I learned that, on the contrary, Deng and his family were not only displeased with my remarks, but extremely angered by them. This was beyond what I could have foreseen. Exactly why did Deng get the idea that I had intentionally pushed him into confronting the public, while I was evading my own responsibilities? I have yet to learn who it was or how that person managed to provoke Deng.<br />
<br />
My intentions were good: to maintain and to protect his prestige, and to do my part in bearing the responsibility. However, it unexpectedly resulted in a great misunderstanding and caused him to feel that I had intentionally hurt him. I indeed feel deeply aggrieved by this affair. I could have chosen to do nothing at all. In fact, it had been unnecessary. I truly,<br />
deeply regret it.<br />
<br />
Why have I placed such special attention to this matter? Because other issues were caused by a difference of ideas and viewpoints. Since I had persisted with my position, even my dismissal from the position of General Secretary was understandable. I started with only good intentions. No matter what kinds of differences I had with Deng over the June Fourth issue, it was a difference of political opinions.<br />
<br />
Before the June Fourth incident, I had always felt that, overall, Deng had treated me very well and shown a lot of trust in me. It is a Chinese tradition to value integrity of character and faithfulness in our relationships. If I had given Deng the impression that I had diverted blame in the midst of a crisis, then not only was this a profoundly false impression of me, but it might cause him deep unhappiness or even emotional pain. The thought of a man of his years, perhaps soon to leave this world, suffering from such an impression was truly unbearable to me.<br />
<br />
Therefore, I wrote to Deng on May 28 specifically to explain my remarks to Gorbachev. However, I told him of only one of my considerations, which I mentioned before, that I was asserting that the summit was officially between Deng and Gorbachev, and because of this, I had naturally commented that Deng was still the main decision maker. I did not mention my second consideration, that is, to refute the popular view that he was power hungry, continuing to control the Politburo Standing Committee even though not a member of it. Amid this public criticism, some kind of explanation had been necessary. There was no reply to the letter I sent.<br />
<br />
I still hope that before he leaves this world (this is what I wrote down seven years ago (in 1992)), he comes to understand the true intentions of my remarks to Gorbachev. Not because after knowing this he might relax anything related to my case: I have no such wish. I know that even if he knew the truth, he would not relax a thing. I only want Deng to know that, having received his longtime trust and vigorous support, even though I refused to accept his decision of cracking down on the student demonstrations, I am not a man who would sacrifice others to protect myself in a crisis.<br />
<br />
I believe that with such an understanding of the situation, he would feel better. I am truly unwilling to see him leave this world with this misconception. Yet I know the chances of his understanding this are very, very slim.<br />
<br />
<br />
<i><p align="right">Deng died in February 1997. Zhao never saw him again after 1989.</p></i></div>

							
							
							
							
