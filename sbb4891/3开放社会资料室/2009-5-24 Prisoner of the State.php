													<h2>1: The Student Protests Begin</h2>
						
						
																											<div id="postmessage_100954" class="t_msgfont"><a href="https://docs.google.com/Doc?id=dmrm5wh_66h48nphr&amp;hl=en#1:_The_Student_Protests_Begin" target="_blank">https://docs.google.com/Doc?id=d ... dent_Protests_Begin</a><br />
<br />
PART 1: THE TIANANMEN MASSACRE<br />
<br />
<br />
 <br />
1: The Student Protests Begin<br />
<br />
<br />
 <br />
&nbsp; &nbsp; The student movement of 1989 is one of the defining moments of Zhao Ziyang's career. On April 15, news of the death of Hu Yaobang, the liberal reformer who had been ousted from his position as Communist Party General Secretary two years earlier, sets off an outpouring of public mourning by college students in Beijing. It is a clear act of defiance against the decision made by paramount leader Deng Xiaoping and other Party elders to expel Hu.<br />
 <br />
&nbsp; &nbsp; The protests come at a time when China's citizens are already worried about rising prices and growing corruption in the country's half-reformed economy. As a result, hundreds of thousands of Chinese join in the demonstrations.<br />
 <br />
&nbsp; &nbsp; The Communist Party leadership is split. The conservatives who had supported the toppling of Hu argue for a crackdown. But Zhao, who had succeeded Hu as Party chief, worries about the political consequences of a server response, and that a hard-line backlash could derail economic reforms. As the protests drag on, the power struggle intensifies.<br />
 <br />
&nbsp; &nbsp; Soon after the protests erupt, however, Zhao is due to travel to North Korea on an official visit, which limits his ability to influence the Party's response to the demonstrations. While he is away, on April 26, the government authorizes publication of an official verdict on the protests, in the foorm of an editorial in the People's Daily. Its strident tone only makes things worse and diminishes Zhao's ability to manage the situation.<br />
 <br />
&nbsp; &nbsp; Here Zhao speaks in depth for the first time about the source of the protests. He explains why he felt they didn't pose a direct threat to the government and how they could have been resolved long before the violent suppression of June 4.<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; Seven years ago [in 1992], I jotted down some notes about the events surrounding the June Fourth incident because I was worried that I might start forgetting some of the specifics. I hoped that it might serve as a hind of historical record.<br />
 <br />
&nbsp; &nbsp; Now I will talk about the incident according to these notes. Some of these issues were covered in the speech I delivered at the Fourth Plenum of the 13th Central Committee [held June 23-24, 1989, when Zhao, outsed from power, defended his role during the protests], but there are also oother issues that I did not mention then. I will now talk about all of them.<br />
 <br />
&nbsp; &nbsp; First, I would like to talk about what initially triggered the student protests. All of the early incidences of student protests were related to the commemoration of [Hu] Yaobang.<br />
 <br />
&nbsp; &nbsp; Yaobang died on April 15, 1989. Immediately after the announcement was broadcast, some college students initiated commemoration activities. Soon thereafter, they took their activities onto the streets, and the number of participants grew and grew. Though at this point some students made some extreme statements because of piqued emotions, overall their activities were fairly orderly and nothing excessive took place.<br />
 <br />
&nbsp; &nbsp; On the nights of April 18 and 19, several hundred people gathered outside Xinhua Gate [outside the Party's headquarters]. I later called for and watched the video recordings made by the Ministry of Public Security. In the so-called &quot;incident of students besieging Xinhua Gate,&quot; some of the students in the front were in fact shouting repeatedly, &quot;We must maintain order! Don't do anything out of line&quot; There was a large crowd of spectators behind them. The students made verbal demands, including demands to meet certain members of the leadership. Then people pushed from behind and it got a little bit chaotic. The students then organized a team to act as guards to keep back the crowd of spectators.<br />
 <br />
&nbsp; &nbsp; On April 22, while the officlal memorial service for Hu Yaobang was taking place, tens of thousands of students were assembled in Tiananmen Square. This had been officially approved. The loudspeakers in the square broadcast the audio from the official memorial service inside the Great Hall of the People, so they could all listen in.<br />
 <br />
&nbsp; &nbsp; This was the situation before the publishing of the April 26 editorial in the People's Daily.<br />
 <br />
&nbsp; &nbsp; Why did the students react so strongly in commemorating the passing of Hu Yaobang? The reasons were complicated.<br />
 <br />
&nbsp; &nbsp; First, Hu Yaobang had always had a very good public image. He was responsible for reversing numerous cases of unjustified persecutions following the Mao years; he had always been a proponent of reform; most important, he was incorruptible while in power. There was a lot of dissatisfaction with corruption back then, so commemorating Hu Yaobang provided a chance to express this discontent.<br />
 <br />
&nbsp; &nbsp; Second, many people were displeased or even outraged by Hu Yaobang's demotion in 1987. Many people were averse to the Anti-Bourgeois Liberalization Campaign [launched in 1987] and continued to be opposed to it. In addition, people found unacceptable the way in which the leadership was changed. In general, people were expressing a felling of indignation over how Hu Yaobang had been treated.<br />
 <br />
&nbsp; &nbsp; Third, when the government's reorganization was proposed in the fall of 1988, programs for reform had been cut back on all fronts. No action had been taken on political reform while economic reform had been brought to a standstill or even retracted. Students were dissatified with the general situation and were expressing their desire for advancing reforms through their commemoration of Hu Yaobang.<br />
 <br />
&nbsp; &nbsp; There were three kinds of people who took to the streets to protest: the vast majority of people belonged to the category I described above. <br />
There were also those who held grievances against our past policies and were taking the opportunity to make some noise. Of course, there was<br />
also a small number of people who opposed the Part and opposed socialism that were hoping to aggravate the situation.&nbsp;&nbsp;<br />
&nbsp; &nbsp; At a Politburo Standing Committee (PSC) meeting (the date is unclear), I said that we should not forbid the activities of the students who <br />
were merely holding their own commemorations while the Central Committee was holding memorial services. There was no reason why we<br />
should reserve for ourselves exclusive rights to commemorate Hu, while forbidding the students to do so.<br />
 <br />
&nbsp; &nbsp; I suggested we punish according to law only those who engaged in the five type of behaviors: beating, smashing, looting, burning, or trespassing.<br />
In all other normal circumstances, there should be an attempt to reduce tensions.<br />
 <br />
&nbsp; &nbsp; After the official memorial service for Hu Yaobang, I proposed a course of action with three points:<br />
1.With the memorial service now over, social activities should return to normaL. Students need to be persuaded to discontinue their street demonstrations and return to their classes.<br />
 <br />
<br />
(At the time, I felt that whatever their motives, the students had in fact engaged in nothing more than commemorating Hu Yaobang. So with <br />
the memorial service over, and their having had a chance to. participate by holding their own activities, there should have been no reason to continue the demonstration&quot;s. It was time to return to classes.)<br />
 <br />
2. According to the principal goal of reducing tensions, dialogue should be conducted at multiple levels, and through various channels and formats to establish mutual understanding and to seek a variety of opinions. Whatever opinions they held, all students, teachers, and intellectuals should be allowed to express themselves freely.<br />
3. Bloodshed must be avoided, no matter what. However, those who engaged in the five kinds of behavior-beating, smashing, looting, burning, and trespassing-should be punished according to law.<br />
&nbsp; &nbsp; My suggestions were all accepted by (Premier) Li Peng and every member of the Politburo Standing Committee and were officially documented.<br />
The above assessment of the situation and the principles for action agreed upon were disseminated via varous channels to local government<br />
<br />
branches. These were the three points that I proposed before my visit to North Korea. I spoke to key leaders of the Central Committee about them<br />
<br />
while taking the elevator down after the memorial service, and later expressed them again formally.<br />
 <br />
&nbsp; &nbsp; On the afternoon of Apri 23, as I was preparing to leave Beijing train station to head for North Korea, Li Peng came to send me off. He asked<br />
<br />
me if I had anything further to add. I said that my position had been summarized in those three points. I later heard that Li Peng reported the three<br />
<br />
points to Deng Xiaoping, who also expressed his agreement. <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; There were no disagreements from members of the Politburo Standing Committee, at least not openly. I can remember only one: on the evening of April 19, Li Peng called me unexpectedly and demanded accusingly, &quot;The students are trying to break into Xinhua Gate! Why aren't any counteractions being taken?&quot; I told him that (PSC member in charge of security) Qiao Shi was immediately responsible, and that he should be able to take care of any urgent situation that might arise using existing emergency plans.<br />
<br />
&nbsp; &nbsp; I later informed Qiao Shi of Li Peng's call. In fact, by the morning of the 20th, most of the students had already left Xinhua Gate. The few who<br />
remained were cleared away by the police. They were ordered onto buses that drove them back to their schools. <br />
<br />
&nbsp; &nbsp; This was the situation of the student demonstrations before I visited North Korea, and the policy of the Standing Committee at that time.<br />
