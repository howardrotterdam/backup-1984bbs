娄烨 颐和园 什么是残酷青春？<br />
<br />
　　 文革时有人看手抄本《少女之心》，稍后有人看足本的《废都》，再后就是《上海宝贝》。青春期的人有这样的逆反心理，什么是禁止的，就要看什么。无论是在别人面前充做谈资的虚荣心，还是满足好奇心猎奇。<br />
　　<br />
　　 现在流行看禁片。色戒刚上，有人为了看梁蛋蛋打火车去香港看幸亏有互联网这个好东西。《色戒》梁朝伟的倾囊而出，《苹果》中的老牛吃嫩草，我都看了。炒作。真的没什么。最多只能算是《金瓶梅》中“×××××（删多少字）”的不良段落。<br />
　　<br />
　　 和娄烨的颐和园相比，差远了。<br />
　　<br />
　　 幸亏某人博克说了下载链接，让我有机会看片。不然除非以后我有资格看内部批判片，否则我一辈子都会以为这《颐和园》就是CCTV6放过的科教片。<br />
　　<br />
　　 拍了禁片，代价惨重。与《鬼子来了》类似，《颐和园》，因为违规参加评选，娄烨5年能不允许拍摄电影。<br />
　　　　<br />
　　 1点01G，2个小时20分钟。貌似是给电影发行管理方审片的版本，开头写着“制片人提醒注意，影片资料内容保密。仅供与此有关指定工人人员使用”。<br />
　　<br />
　　 故事很简单，一个漂亮的青春女生，1987年考上北京的大学，经历了疯狂的1989，越过2000年。2003年与从柏林回来的大学恋人相遇。但是同样经历了很多变迁的大学恋人突然在见面之后离去。从此之后，两人所处的地点虽然并不遥远，却从不相见。<br />
　　<br />
　　 喜欢的人会很喜欢，不用多说。吸引人眼球的要素都会具备：性爱＋青春毁灭＋特殊年代的事件。还有很不错的音乐。复古的风格，无论是黑豹的Don't break my heart,还是罗大佑的青春舞曲，都是有深深的涵义。<br />
　　<br />
　　 不喜欢的人可以用无数的语言来质疑这部影片：比如特别文艺腔的女主角内心独白，特别晃的镜头，特别粗糙的胶片画面，特别多的露点。和《颐和园》相比，色戒的床戏根本不算啥。因为在欧洲，颐和园是21岁以下禁止观看。<br />
　　<br />
　　 当然还有特别多的不知所云的，或者说只是描述个人感受的意识流台词：“如果不是在一种理想中来考察我的生活，那么生活的平庸将使我痛苦不堪。而在我怀有这种念头的时候，我们碰见了。”<br />
　　<br />
　　 文学青年风格叙事般的叙事方式除了豆瓣，已经很少了。<br />
　　<br />
　　<br />
　　 我有一个痛苦，就是不能早生几年。赶上壮怀激烈的80年代。因为在此刻，我总是觉得没有人能特别理解我。（也许现在的每个人都这么想），然后我就会拒绝和别人进行交流碰撞。<br />
　　<br />
　　 但是80年代不会。这是一个喜欢思辨，谈论的概念的时代。大学生可以毫无顾忌地看哲学，抽烟，谈论诗歌，他们生活中至少还有理想残存，还有信念依稀。他们可以讨论很多形而上的问题，来求得心灵的暂时安宁。而不至于被人反问一句：“哥们，有病吧？”<br />
　　<br />
　　 经历过那个时代的导演会刻意美化那个年代，美化理想的地位。就好像变形金刚的票房是我们80后创造的一样。经历一个时代，必然有感同身受。<br />
　　<br />
　　 一个经历过复杂时代，经历过时代变迁的人始终会比单一成长环境下的一代成熟。思考问题，绝对不是出生在物质充裕的独生子女所能理解的。即使因为环境被迫早熟的我，从那个时代来打量，还是一个不可信赖的80后。<br />
　　<br />
　　 那是一个特别思辨的时代，又是一个特别“傻”的时代。没有那么多的我，精神上应该还有一片净土。你可以暂时停下来思考下，不用无时不刻向前向前。<br />
　　<br />
　　 还有这样的女生。郝蕾扮演的这种类型女生。她们漂亮，早熟，多情。在青春期过早经历了轰轰烈烈的恋爱，（比如说娄烨《苏州河》里一直寻找马达的周迅），但是这样的女生往往成为了一个蛾子，最后只有或是被灯火吞噬，或是为了轰轰烈烈的青春付出了惨痛的代价。<br />
　　<br />
　　在无数的寂寞青春期，我一直幻想能河这样的女生谈一场轰轰烈烈的恋爱。但是越往后长大，我就发现电影描绘的也好，现实生活也好，这样的女生往往会越来越不幸福。当一次次把感情投入的时候，那么最后必然会产生恐惧与害怕的情绪。就如同郝蕾在电影里所描绘的那样，忽而歇斯底里，忽而呆若木鸡，忽而判断力全无。<br />
　　<br />
　　但就像鲁迅先生所说的，美好的东西被摧毁特别能震撼人。当年被全班同学谈论最多的女生却草草嫁人，暗淡地结束了自己的青春的时候，当把一个人的成长和一个时代相联系的时候，当一个大学生在1989年曾经壮怀激烈充满光芒的眸子开始黯淡时，当一个满怀理想的漂亮女生成为一个家庭主妇的时候，这真让人感叹。<br />
　　<br />
　　<br />
　　 这大概就是残酷青春的文艺电影，它和大片不一样，它强调的不是你“看到了什么”，而是你想到了什么。不是构造一个侏罗纪或者一只怪兽让你啧啧称奇，而是让你发散思维，给你很多思考的空间。甚至是你能看着看着就看到自己曾经的影子，然后潸然泪下。<br />
　　<br />
　　 你可以不用去纠缠什么细节，比如为什么男主角的蓝衣服跨越了2个国家，几十年。我随便可以给你一个象征主义的说法，比如说象征他不曾改变的内心什么的，但是你必须心地敏感。<br />
　　<br />
　　 所谓青春影片之所以感人，只是因为曾经的自己。讲到最后，我们难过不是因为电影主角的命运，而是因为他描绘了古今中外都共通的道理――过去我们曾经做了很多傻事。我们害怕见到过去的自己。我们努力做很多我们认为正确的事，最后还是一无所获。<br />
　　<br />
　　 平平淡淡终其一身，很不情愿地忘记年少时的梦和很多誓言，泯灭忘记。<br />
　　<br />
　　 片名为什么叫颐和园？娄烨说：我认为余红和周伟最美好的瞬间是在颐和园湖面上泛舟的场景，虽然这个场景很短。<br />
　　<br />
　　 好花不常开，好景不常在。何日君再来？</div>

							
							
							
							
