我是植物人的评论&nbsp; &nbsp; <br />
<br />
　　 昨天去了《我是植物人》北京首场点映现场，没想到这部宣传力度并不那么大的电影，吸引了如此多的观众。而且与以往观影人群不一样的是，这次观影来了不少外国朋友。在我前排的一对外国情侣，在电影放映途中，不停地发出笑声和感慨，我突然想到了一句话，好电影，无国界！ <br />
　　 总的来说，这部电影，给了我太多的惊喜和触动。你会发现，“我是植物人”这5个字，传递出的，不仅仅是这部电影中说代表的含义，正常的人，用了假药，变成了植物人。而是在告诉我们，我们生活在这个社会，我们想做的，我们不能做，我没能做的，我们还是没有做，我们已经渐渐麻木于一条惯用的生存模式。是不是到最后，我们发现，我们连最后一点良知都没有了，我们已经不是属于人这个种族了，那该是是多么的荒谬和可笑！ <br />
　　 假药、假奶粉、假文凭、什么都是假的，不知道什么时候有假你妹？一堆堆问题层出不穷，反正没有到自己的头上，也就当着饭后谈资。若是一天到了自己头上，没钱没势，我也不相信你能为自己争得多少权益。 <br />
　　 记得导演最后说的几句话，凭我们的努力，来改变我们的状况，那是必然不能的。但是我们在想尽一切办法，发现问题，提出问题，从而呼吁那些能解决问题的人们，来解决这些问题，这就是我们目前所能尽到的最大努力。 <br />
　　 制片人谢晓东，犀利的言语，我借用一句，这个社会总得有一个底线！<br />
