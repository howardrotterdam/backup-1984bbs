																											<div id="postmessage_197919" class="t_msgfont">董乐山:<br />
<br />
是预言，还是现实？<br />
<br />
1983年我在美国康奈尔大学访问快要结束的时候，一天下午忽然接到政治系法国访问教授弗朗西斯&#8226;罗森斯梯尔博士打来的电话，约我与他一见。原来他听说我曾研究过英国著名作家乔治&#8226;奥威尔，又翻译了奥威尔的政治寓言小说《一九八四》，就亟谋一晤，因为他本人是设在法国斯特拉斯堡的欧洲议会的政治研究室主任，正在筹备一次学术会议，由欧洲议会出面，约请世界各国学者，在1984年春到斯特拉斯堡相会，探讨奥威尔在1948年写的这部作品，到了他所预言的1984年究竟有哪些预言已经应验了，同时估计一下这部作品在思想界的影响。为了消除一般人对《一九八四》的政治误解，表明其中所描绘的不仅仅是影射苏联这样的国家和制度，而是人类社会的共同性的弊端，这次会议所邀请的不仅有西方各国的持各种政治见解的学术界人士，而且也邀请了苏联和东欧国家的学者。可以想见，这样的邀请遭到了后者的拒绝，只有南斯拉夫的学者欣然同意参加。因此罗森斯梯尔博士无意中发现同在康奈尔大学访问的中国学者中，居然有一位奥威尔作品的译音，他的欣喜和亟谋一晤的迫切心情是可想而知的。<br />
对于罗森斯梯尔博士的诚意，我是没有疑问的。因为在我们约晤的短短两个小时中，我发现他不仅是个一般意义上的政治学家，而且也是一位对人类的前途忧心仲仲的有识之士。在当今世界人类面临的许多问题上，有不少真知灼见。因此与他相识，是我在美国年馀逗留的一个意外收获。遗憾的是我当时正在打点行装，准备回国，而在回国之后，又不幸身罹顽疾，卧病在床，尽管收到了欧洲议会政治研究室发来的正式邀请电报，却无法赴约，参加这个盛会，与世界各国学者一起探讨人类的前途，不能不说是一桩憾事。<br />
就在我患病卧床期间，我又听说奥威尔的儿子曾携根据他父亲著作拍摄的影片《一九八四》来华，向我国电影界作了内部放映，我没有机会看到这部影片，也是一桩憾事。<br />
在国外，1984年新年伊始（不，其实在1983年我还在美国时就已开始了），西方各国主要报刊就纷纷在显要地位刊登文章，或发行专辑，一些电视台也邀请文化界和思想界的著名人士发表讲话，评述奥威尔在三十四年以前出版的这部小说的意义。欧洲议会的学术讨论会和影片的放映是这些活动的顶峰。在这样的多方面的重视中，《一九八四》又再版发行了一百多万册，再次成了一部畅销书，这在英美出版史上是一件罕见的事。<br />
那么，究竟是什么原因，使得《一九八四》在西方文化界和思想界这么受到重视，它的作者乔治&#8226;奥威尔究竟是什么人呢？<br />
乔治&#8226;奥威尔是英国作家欧里克&#8226;布莱尔的笔名。欧里克&#8226;布莱尔于1903年生于印度孟加拉省的一个低级英国官员的家庭，照他自己的说法，是属于“下层中产阶级”。他在英国的寄宿学校毕业后，十八岁就到缅甸参加英帝国警察部队，目睹了英国殖民统治的种种弊端，到1927年就辞职回国，开始他的写作生涯。虽然在这以后的十年中，他用乔治&#8226;奥威尔的笔名写了三部小说和不少随笔和评论，但是他在英国文坛并不出名，生活困苦，病魔缠身。这样的出身和经历，对于他在作品中采取同情殖民地人民和英国下层群众的态度以及表现悲观情绪，恐怕不是没有关系的。1937年奥威尔像当时一些英美左翼作家一样，到西班牙去参加了内战中以人民阵线为领导的政府军一方，他在缅甸当警宫时所受的军事训练这时发挥了作用。但是也正如当时许多参加志愿军的自由主义知识分子一样，他对人民阵线内部的各个左派政党与派系之间的斗争发展到武装内讧感到了失望，发现自己成了“两种政治理论之间的大规模斗争中的一个走卒”。这时正好他颈部中了一弹，康复后就退出了这场内战和内战中的内战。<br />
西班牙内战的经历，使奥威尔对极权主义（不论是法西斯主义的还是其他形式的）感到了极端的憎恶，他的出身和经历又使他对整个人类社会前途产生了悲观和绝望的情绪。这部作品写于他因肺病去世前两年的1948年，书名只是把四八做了颠倒，换句话说，奥威尔预见到令人毛骨悚然的未来社会就会在三十六年之中出现，这也充分说明了他的悲观之深。<br />
《一九八四》在奥威尔死前的1949年出版，立即轰动一时，消息不胫而走，成为畅销一时的名著，乔治&#8226;奥威尔也因此声名大噪，这大概是这个穷途潦倒的作家所始料不及的。<br />
用政治寓言方式写“反面乌托邦”的预言小说，在西方并不以奥威尔始，近的有阿尔杜斯&#8226;赫胥黎的《美丽新世界》（1932），远的有叶夫根尼&#8226;扎米亚金的《我们》（1923），还有杰克&#8226;伦敦的《铁蹄》（1909）和H．G．威尔斯的《入睡者醒来的时候》（1899）等等，为什么惟独奥威尔的《一九八四》发生这样大的影响，而且其影响至今不衰呢？这当然是与战后美苏两个超级大国为了争霸世界而掀起的冷战方兴未艾分不开的。在当时的一部分人的心目中，奥威尔也是被视作一般的反苏作家而不屑一顾的。然而在三十六年后的今天，我们再来回顾一下这一个时期世界形势的变化，就会发现奥威尔所阴暗地预言的人性泯灭，六亲不认，观念颠倒，谎言当作真理，自由遭到剥夺，思想受到控制，行动受到监视的现象，在当今世界上几乎是普遍存在的，只是程度上略有不同而已。水门事件的监听活动，不是曾经引起过美国报刊惊呼“老大哥在监视我们”吗？西方一些国家（包括日本在内）始而同意资助《一九八四》电影的拍摄，继而以修改内容为条件，不是证明它们都感到该书也有影射、讽喻它们的地方吗？因此，在经过三十六年之后，再把《一九八四》看作是仅仅影射苏联一国的看法，未免过于褊狭，这样就无法解释这部作品在当今世界上的普遍价值和继续发生影响的原因。<br />
因此，从现实意义来说，奥威尔写作《一九八四》的用意要比当初人们所主观臆想的广泛和深刻得多。他是在向整个人类社会提出警告。要人们注意到社会（不论是什么主义的社会）若是任其官僚比，人就会成为一个庞大的政治官僚机器的一部分，丧失了个人的意志，这就是《一九八四》所展示的阴暗的前途。1984年来了又去了，《一九八四》中所作的预言有没有不幸而言中，或者在多大的程度上已经应验了，凡是稍有头脑的人是不难自己得出结论来的。<br />
把《一九八四》介绍给中国的读者，还有一个语言学上的原因。由于《一九八四》的畅销和深远的影响，书中的一些人物形象（如“老大哥”）和杜撰的语言（如“新话”、“双重思想”等等）已经成为英美文学作品和政论文章中所经常引用的文学、政治典故，甚至收入了一些权威性的词典。因此，不仅是为了帮助从事理论工作的同志了解一些西方政治观点，需要译介这本书，就是为了让一般阅读英语报刊书籍的读者增加一些文学典故和语言知识，这本书也有译介的价值。<br />
但是应该指出，《一九八四》作为文学作品，在写作上并不怎么出色。它虽是一部tour de force（力作），但结构简单，性格刻画浮浅，政治议论过多，读来令人枯燥乏味，文学形式只是一层薄薄的外衣而已。因此它在文学界的影响远远不如思想界那么大。反而是奥威尔当年没有引起世人注意的文学作品（如《在缅甸的日子》等），在《一九八四》获得成功之后，引起了文学界的注意，其中许多章节和段落正被选入中学语文课本，作为范文来读，这大概也是奥威尔生前所始料不及的。</div>

							
							
							
							
