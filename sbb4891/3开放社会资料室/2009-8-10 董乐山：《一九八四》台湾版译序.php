													<h2>董乐山：《一九八四》台湾版译序</h2>
						
						
																											<div id="postmessage_197670" class="t_msgfont">要向台湾读者介绍乔治&#8226;奥威尔，我想是要比向大陆读者作介绍容易得多。原因很简单，这中间少了不少政治上的顾忌。<br />
但是临到真正提起笔来，虽然说不上重如干钧，却确有无从下手之感。这大概是因为几十年的人为隔阂在交流上所造成的生疏感所致。<br />
为省事起见，最易落笔的还是作者的生平，那就从这里开始吧。<br />
乔治&#8226;奥威尔（George Orwell，1903―1950）是英国作家欧里克&#8226;亚瑟&#8226;布莱尔的笔名。1903年生于印度孟加拉省一个英国中低级文官的家庭，照他自己的说法，是属于“下层中产阶级”。他在英国的寄宿学校和伊顿公学靠助学金毕业以后，由于家境贫寒，供养不起他上牛津或剑桥大学，遂弃学从“军”，十八岁就到缅甸参加帝国警察部队。在伊顿公学遭到贵族富家子弟的冷落，在缅甸目睹英国殖民统治的种种弊端，以及在大萧条下在英国体会到的下层生活的困苦，使他在1927年辞职回国从事写作后信奉了社会主义。在这以后的十年中，他用乔治&#8226;奥威尔的笔名写了三部以英国下层社会生活为背景的小说和不少随笔及评论，但在文坛上默默无名。1937年西班牙内战爆发，奥威尔像当时一些左翼作家一样，到西班牙去志愿投效共和政府一方，抵御佛朗哥叛军的进攻。但是在志愿军的国际纵队里，左派派系却忙于内争，甚至发展到武装内讧的程度，这使他觉得自己成为“两种政治理论之间的大规模斗争中的一个走卒”，腹背受敌，在失望之馀便趁颈部中弹回国治疗之机，退出了这场内战中的内战。<br />
但是参加西班牙内战的经历，对奥威尔来说不是一点也没有收获的；这使他看清了国际上假各种主义之名行极权主义之实的政治势力，与法西斯主义或纳粹主义并无二致。他对极权主义（不论是法西斯主义、纳粹主义或史达林主义）既感到极端的憎恶，但是他的出身和经历又不能安于英美的现状，因此找不到令他满意的出路，这使他对整个人类社会的前途产生了悲观和绝望的情绪。就是在这种灰色情绪的支配下，他写出了撼世之作《一九八四》，书名只是把他在患肺病去世之前成书的1948颠倒了一下。换句话说，奥威尔预见到令人毛骨悚然的未来社会将在三十六年内实现，这充分说明了他悲观之深。<br />
《一九八四》是所谓“反乌托邦三部曲”之一（其他两部是白俄作家叶&#8226;扎米亚金的《我们》和英国作家赫胥黎的《美丽新世界》），这是与资本主义萌芽期间出现的“乌托邦三部曲”（即托玛斯&#8226;莫尔的《乌托邦》，康帕内拉的《太阳城》，安得里亚的《基督大都会》）中空想的乐观的憧憬相对而言的，它们对人类社会前途作了悲观的预测和描绘。尤其是《一九八四》所采用的梦境般的笔法，即使在局外人读来也感到毛骨悚然，使人透不过气来。而早在1984年之前，远远不仅限于作者所影射的苏联一国，《一九八四》所反映的阴森森的极权主义统治就早已成为现实了；人性遭到了泯灭，自由遭到了剥夺，思想受到了钳制，感情受到了摧残，生活的贫乏和单调就更不用说了，个人完全成了一个庞大的官僚主义化社会中一个自动化的机器，尤其可怕的是人性已经堕落到为了生存而是非不分的程度。<br />
作者在书中设想到了1984年时，世界上只剩下三个超级大国：欧亚国、大洋国和东亚国。三国互相攻战不已，永远是拉一个打一个，不是与这个结盟打那个，就是与那个结盟打这个。因此无论哪个国家，国内都长期处于战争状态，物资匮乏，一切实行配给和管制，甚至精神生活也不例外。书中主人公温斯顿&#8226;史密斯在政府的真理部工作，这个部负责控制新闻、娱乐、教育和艺术，名为真理部，实为谎言部。温斯顿&#8226;史密斯每天的工作就是编造谎言，修改谎言，甚至修改和涂抹历史记录及人们的记忆。正像和平部实际上是战争部，友爱部实际上是镇压部，富裕部实际上是匮乏部一样，在这个是非颠倒的世界里，统治党的口号是：战争即和平，自由即奴役，无知即力量，也就不足为奇了。几十年来我们生活在这个谎言的世界里所耳濡目染的不正是这样吗？<br />
正是在这样一种事事受到管制和监视的窒息气氛中，在人人不敢以诚相待的猜忌环境中，连在日记中也不敢写真话的温斯顿&#8226;史密斯与他的同事裘莉亚发生了爱情，两人偷偷地在野外相会，享受爱情这禁果的滋味。然而，这种偷偷摸摸的爱情毕竟是短暂的，温斯顿&#8226;史密斯终于遭到了逮捕。这并不是由于他同裘莉亚接触时说了心里话，也不是由于他想参加什么地下反抗组织，而是由于他不相信自由即奴役的统治哲学。为了要把他的独立思想改造过来，当局在狱中对他施行了种种精神上的酷刑，待他完成了这一过程终于获释时，他已成为一具自觉的行尸走肉了。<br />
奥威尔没有在一个极权主义统治下的社会中生活过，可是他对这种生活的描绘，不论在总的气氛方面、政治生活的细节方面、人的内心世界方面……都达到了栩栩如生、呼之欲出的程度，仿佛他的体会比身历其境者还要细腻，还要深刻，这由不得你不能不承认这是20世纪的一部划时代的tour de force（力作）。他在1948年所预言的一切，在1984年以前几乎全部实现了，至少在当今世界的相当一部分地方，这恐怕是他在生前所没有预料到的，在1984年以后的1990年，世界上绝大多数的极权主义国家发生了天翻地覆的变化：一个个极权政权像多米诺骨牌一样纷纷倒下。民主和自由毕竟是人类共同的理想；20世纪开始时出现的极权幽灵，在世界上徘徊了快一个世纪以后，终于气数已尽，即将成为人类历史的陈迹。但是人类社会为此付出的代价是何等高昂！<br />
愿奥威尔的《一九八四》即使在事过境迁之后，仍能作为历史的明镜，永远为我们，为我们的子孙后代，作为借鉴，作为警惕。<br />
何况这场噩梦还远远没有过去。<br />
<br />
1991年1月2日</div>

							
							
							
							
