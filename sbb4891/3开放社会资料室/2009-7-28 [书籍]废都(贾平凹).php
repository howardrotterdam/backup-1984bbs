																											<div id="postmessage_181413" class="t_msgfont">博主按：贾平凹的长篇小说《废都》在被禁17年之后，终于解禁了，有兴趣的可以到书店购买一本正版收藏。<br />
PS：这本电子书是足本，未删节。<br />
<br />
资源格式：PDF<br />
书名：废都<br />
作者：贾平凹<br />
出版日期：1993年<br />
出版社：北京出版社<br />
出版地：中国大陆<br />
文字语言：简体中文<br />
<br />
书籍简介：<br />
1993年，贾平凹的《废都》在《十月》杂志连载，后由北京出版社出版，首印50万册。这本描写当代知识分子生活的世情小说，由于其独特而大胆的态度以及出位的性描写，引起社会各界广泛关注。一时间洛阳纸贵。业内人士告诉《t望东方》：“当时出版社甚至用了卖版型的方法，以近百万的价格将《废都》版型卖给六七家出版社。这是相当惊人的。”<br />
<br />
据不完全统计，正式和半正式出版的《废都》有100多万册。而盗版大约超过了1200万册！<br />
<br />
在《废都》中，作者贾平凹写出了一部80年代的中国社会风俗史。采用了中国古典的草灰蛇线手法，而融入了西方的意识流和精神气质，中西合璧。《废都》也创造了一种新的语言，这在文学史上是不可多得的。 作者以主人公庄之蝶为中心巧妙地组织人物关系。围绕着庄之蝶的四位女性――牛月清、唐宛儿、柳月、阿灿是小说中着墨最多的。她们分别是不同经历、不同层次的女性，每个人的际遇、心理都展示着社会文化的一个侧面。<br />
<br />
作者简介：<br />
贾平凹，原名贾平娃，1952年出生，陕西丹凤人。1975年毕业于西北大学中文系。全国政协委员，陕西省作家协会副主席，西安市人大代表，西安市作家协会主席。1974年开始发表作品。著有小说集《贾平凹获奖中篇小说集》、《贾平凹自选集》，长篇小说《商州》、《白夜》，自传体长篇《我是农民》等。《腊月・正月》获中国作协第3届全国优秀中篇小说奖；《满月》获1978年全国优秀短篇小说奖；《废都》获1997年法国费米娜文学奖；《浮躁》获1987年美国美孚飞马文学奖，最近获得由法国文化交流部颁发的“法兰西共和国文学艺术荣誉奖”。<br />
