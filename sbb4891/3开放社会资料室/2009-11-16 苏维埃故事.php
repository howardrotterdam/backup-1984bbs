																											<div id="postmessage_339818" class="t_msgfont">　　视觉上最震撼的，有两个，一是纳粹阅兵和红场阅兵，在步伐、神态与审美观上，如出一辙。二是影片特别将几幅纳粹宣传画，和斯大林时代的宣传画放在一起，看上去就像抄袭。<br />
<br />
　　二战后，苏维埃在立陶宛、拉脱维亚等地区，大量驱逐原住民，为改变人口结构，把他们流放到西伯利亚。半个世纪后，这位拉脱维亚导演利用大量解密档案，拍了这部纪录片。末尾说，苏维埃政权杀死了2千万男人和女人，这片子是为他们拍的。尽管关于苏维埃不堪回首之往事的倾诉与披露，已汗牛充栋；最近也看了王康先生在阳光卫视的系列节目“苏维埃的兴亡”。王先生的面容越发像列宁了，从他口中述说专制苦难，历历在目，又恍如隔世。就想，什么时候唐国强在电视上批判个人崇拜，必有振聋发聩之效。<br />
<br />
　　相比卡廷森林事件，1932年冬天的乌克兰大饥荒，我以往了解较少。1932年9月11日，斯大林写信给卡冈洛维奇，说“乌克兰的局势严峻，我们若不赶紧采取行为，我们就将失去乌克兰”。几周后，他作出决定，开始停止乌克兰的粮食供应。有两位大饥荒幸存者的描述，说到当年连咸菜、菠菜和种子，都被没收，无数人在家里缓慢饿死。在车站，过于消瘦的人，被赶下了火车。内务部的纠察队，挨家搜集尸体，一具尸体可换200克面包。他们将奄奄一息的人抬走，因为不想明天又来。<br />
　　1932年，乌克兰的死亡人数是66万，1933年飙升到700万人。次年恢复到48万。诺贝尔经济学奖得主阿玛蒂亚森曾说，现代史上，民主国家从未发生过大饥荒，而发生大饥荒的地方，没有一次是因为粮食不足。影片列出了数据，1929年，苏维埃的小麦出口是2.6万吨，到1932年大饥荒时，达到51.8万吨。这是一场不开枪、不用毒气的，针对一个民族的政治屠杀。<br />
<br />
<br />
　　1940年后，陈独秀发表了被称为“最后见解”的几篇文章中，对苏维埃政权持彻底否定的立场。他说，“是独裁制度产生了史大林，而不是有了史大林才产生独裁”。所以，“我们若不从制度上寻出缺点，得到教训，只是闭起眼睛反对史大林，将永远没有觉悟，一个史大林倒了，会有无数史大林在俄国及别国产生出来”。<br />
<br />
　　导演采访了好些欧洲的学者和历史学家。一位索邦大学的教授说，他在很多地方演讲，列出现代政治屠杀的源头，总让很多人惊诧。他列出的就是1849年1月《新莱茵报》上，恩格斯的一篇文章，其中谈到暴力的阶级革命，是历史变迁的主要方式。但那些资本主义不发达的落后地区，是不可能成为革命者的。恩格斯提到了塞尔维亚等地，称这些地方是“种族的垃圾”。他们只可能被消灭在革命中。<br />
　　1851年5月16日，马克思在一篇文章中也说，太脆弱的民族没有存在的理由，他特别提到了波兰。近代以来，最可怕的一种政治思想，就是一部分人被公开地标识为需要被消灭的敌人。在之前的欧洲思想家中，尚未出现这种鼓吹现代政治屠杀的社会思想。<br />
　　这片子引人争议的，就是勾勒纳粹与苏维埃的相似与勾结。1939年的苏德协议倒是是众所周知的，但内务部与党卫军的各种密切合作，尤其是在消灭犹太人方面，还是叫人触目惊心。早期，纳粹主义很喜欢标榜和苏维埃的相似性。戈培尔曾在一篇评论中赞美苏维埃，说“列宁是世界上仅次于希特勒的最伟大的人”。而在德国进攻苏联前，苏维埃的报纸上，也常出现赞扬纳粹主义的文章。<br />
　　左派思想的可怕，就是抹杀了人的灵魂的平等与尊贵。因为他们否定了足以让人的灵魂平等和尊贵的支点。作家萧伯纳很典型，他的言论我读过，但从片子里亲眼见到他大放厥词的演讲，还是直掉眼镜。这位外形和大脑一样睿智的人物，就像说出一个真理一样，说，“每个人认识的人里面都有十二分之一的人是毫无用处的人，他们的存在是社会的负担，我们应该直接要求他们，请证明你存在的价值，你所创造的大过你所消耗的。如果他不能，就不能再让他们活在这个社会了”。1934年，他在伦敦一家报纸撰文，主张科学家们发明一种“人道主义煤气”，可以让这些人毫无痛苦的死去。<br />
　　10年后，纳粹集中营中采用了萧伯纳的建议。直到艾希曼受审时，他都是这样为自己辩护，集中营使用的毒气是“人道主义”的。<br />
　　不过萧伯纳还是反对纳粹。作为一个左派，他认为社会进步需要杀人是必要的。作为一个启蒙知识分子，他认为按“种族”杀人是荒谬的，只有按着“阶级”或“智商”来杀人才可以被接受。当列宁写下这样的文字，“抓住至少100名富农，杀死这些人质，让方圆100公里的人都知道并因此吓得发抖”。左派知识分子有一种本事，似乎很容易把这里的豪情壮志，和纳粹区分出来。可能因为纳粹的根基是一种错误的生物学理论，苏维埃的根基是一种错误的社会学理论。而左派知识分子们生物学都不太好，对社会学却比较自负。<br />
<br />
　　一位法国女学者说，20世纪的这两种制度，都有一种相同的创造“新人类”的野心。纳粹和苏维埃，都不能接受人类卑微和罪恶本性的存在。他们希望以政治的方式，就是暴力的方式，来与人性作斗争。这就是极权制度的本质。这和阿伦特对纳粹与苏维埃的解读，有相似之处。只是阿伦特更多分析的，是民众对极权制度的接受心理。她说这是人的内在孤独决定的。按圣经的表达，可从两方面说，一是人的灵魂在罪性中有无法被填满的孤独；二是人的灵魂中有天然的敬拜和交托的渴望，也就是宗教的渴望，永远都在寻求被填满。所以极权制度都具有伪宗教的性质，独裁者把自己打扮成救世主，在人类精神的熊市上，低价收购世上一切丧失信仰的灵魂。<br />
　　以民族、国家或阶级，将人类区分，几乎是世上所有文明的特征。但现代极权主义，是对这种区分的一次残暴而冷酷的宗教化尝试。换言之，就是关于阶级、国家和民族的一次前所未有的偶像崇拜运动。孔子说，儒家的理想是“四海之内皆弟兄”。所谓四海，就是“九夷，八狄，七戎，六蛮”。四海之内，差不多就是今天的汉族区域。说白了，其实这也是希特勒和斯大林的梦想。<br />
<br />
　　杀人固然是错，但错得最离谱的，是连梦都作错了。亲爱的小书亚，从你开始，和爸爸妈妈另作一个梦好吗。这次的梦想，是四海之外皆弟兄。你的肤色，是一种款式，不是一种诅咒。</div>

							
							
							
							
