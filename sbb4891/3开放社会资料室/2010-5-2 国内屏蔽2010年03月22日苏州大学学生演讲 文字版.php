童话的开头都是：很久很久以前……那我们这本书的开头则是：不远了，不远了，社会主义就要来到了,有4颗牙和40条腿的又是什么东西？ 答：中央政治局。 所谓中央政治局里面有九大长老，就是说有九个人，他们的平均年龄都是在70岁左右。一个国家的儿童如果他不天真的话，那么这个国家的未来是缺乏想象力的； 一个国家的领导人都是70岁的话，那么这个国家是缺乏朝气的，你说对不对？<br />
<br />
人作为不同个体难免会有不同的想法，把强奸变成顺奸很难，把顺奸变成享受更难。科学精神是怀疑，是实证，是挑战，是再证。科学是一种精神，它追求真理。什么是真 理？就是真的很有道理！我们常说颠扑不灭的真理，因为真理的形成恰恰需要一次又一次的颠扑，一次又一次的怀疑，一次又一次的挑战。因此，任何宣称掌握了真 理的言论都是违反科学精神的，任何以真理的名义，以科学的名义排除异己、压制挑战都是对科学精神的污蔑，与宣称掌握真理而将布鲁诺送上宗教审批台的中世纪 野蛮教会无异！<br />
<br />
上天赐给人三种品质 ―- 诚实、聪明和党性。但是一个人不会有两种，因为如果你是诚实和聪明的话，那你就没有党性；如果你是忠诚和有党性的，你就不是聪明的；如果你是聪明和有党性的，那你肯定不会诚实。<br />
<br />
中国到了如今这天地，官僚统治阶级贪赃枉法、腐败透顶，金融业黑洞烂账，资源破坏、环境污染，警匪一家，整个官场就是一个黑社会，房产改革、教育改革、医疗 改革全部失败，成了压在千亿人民头上的新三座大山，两极分化成世界之最，信仰缺失……总之一起都在摸着石头过河。房地产“摸着石头过河”造就了几百个亿万 富翁，他们花天酒地，醉生梦死，造成了上百万的拆迁户，黑白凸现的是既得利益集团与弱势民众的对立。<br />
<br />
还有整整一代房奴。<br />
<br />
但是如果你敢进医院的话，没有个几百块钱是出不来的，医疗纠纷不断。<br />
教育事业“摸着石头过河”，大学生成为继房奴后的第二大被剥削阶级，学术是大跃进的，博士一礼堂，硕士一操场，医疗收钱给点药，大学收钱给张纸。<br />
经济上“摸着石头过河”到处都是假货，有毒食品祸害了中国，还推向世界，丢尽了中国人的脸。<br />
金融业“摸着石头过河”，造就了几百个亿万富翁，股市蒸发二十兆，大户变成散户，散户变成瘪三，一片惨叫声……<br />
<br />
幻灯片文字：<br />
我们抵制日本货，因为他伤了我们的自尊；<br />
我们抵制韩国货，因为他伤了我们的感情；<br />
我们抵制法国货，因为他伤了我们的面子；<br />
我们支持中国货，结果他伤了我们的身体。<br />
<br />
三鹿啊~<br />
<br />
日你先人，这是一个四川话，北京那边叫草你大爷，台湾那边叫我干你娘，它的全称是：我日你先人板板。日你先人板板，我一直不明白板板是什么意思，在古龙小说 里，只要四川大盗一出场他就会说：XXX，我日你先人板板。一直到有一天，一个四川网友告诉我，板板就是棺材板，棺材板上日他先人，这是何等波澜壮阔的场 景。三鹿，我日你先人板板！我们喝你的牛奶原来想变得结实，结果结石了。民以食为天变成民以“添”为食了，三聚氰胺添加剂……<br />
这些大家都知道，总结成顺口溜就是：政府改革机构臃肿，企业改革下岗停工，房产改革口袋掏空，教育改革父母逼疯，医疗改革提前送终。<br />
但是这一切在贪官污吏眼里根本算不上事情，各地各级政府官员根本无需自己买车，他们可以用纳税人的钱去买豪华车，兰德酷路泽~ 烧免费的汽油，干所有的私 事，还不用考虑吃交通罚单，在中国，有哪一天的新闻里没有贪官被双规、司法腐败、环境污染、矿难频发、民怨沸腾 ？ 而这些还是经过中宣部“过滤”、“漂白”之后的新闻呢！<br />
老百姓，或者说是公民，被迫供养着世界上规模最大，机构最臃肿的公务员队伍，每天被“公仆”们在餐桌上吃掉几千亿，买车上千亿，汽油费上千亿！<br />
<br />
喂！人民！服务！<br />
<br />
为什么?为什么放着世界上发达国家的先进社会政治制度不屑一顾，对其他国家的发展经验不用，偏偏要“摸着石头过河”？摸了三十年，整个国家还在河中― - 就是所谓的社会转型期！今年是五四运动九十周年纪念日，九十年来，一代代中国青年一直把“五四”精神当作激励人心的崇高价值，那是因为我们看到五四 运动的一个重要真谛：那就是当政府腐败无能，当国家需要进步，当社会呼唤雷锋，青年人，尤其是中国的学生，总是愿意不惜牺牲，走时时代的前面。<br />
<br />
科学和民主在我国仍然具有现实意义，科学刚刚我们已经说过了，而民主，是实行全民制宪、宪法至上、三权分立、新闻自由、军队国家化、军人不参政、多党竞争、 胜者执政，让全体国民享有充分的民主与自由。我们中国至今还不是一个开放的言论平台，这使得很多似是而非的说法不能得到充分的讨论，比如说“中国不能照搬 西方的民主”就是一个实例。什么叫完全不能照搬西方政治制度？是否意味着我们一点也不能照搬？不能百分之几的照搬？是百分之五还是百分之九十五？这是值得 讨论的吧！但是很可笑很可笑，在国内实行党天下，一党专政，东西南北中，党领导一切，欣赏单极社会的中国，一跑到国际社会上却喋喋不休，叫嚷着要“多极世 界”、“一超多强”，反对美国霸权，要解放世界上三分之二的受苦受难的人民大众，当然了，现正在大力引进外资，叫资本家过来解放我们。<br />
<br />
三 权分立、民选政府、新闻自由、民主政治不适合中国国情，人权是西方的价值观。中国工人的失业叫“下岗”，农民失业叫“农村剩余劳动力”，中国的通货膨胀叫 “经济过热”，美国政府说中国共产党不尊重人权叫“干涉中国内政”，反过来我们指责他们叫反对美国霸权，美国人历数我们的过失，叫反华，反过来我们历数他 们的过失叫维护世界和平。<br />
<br />
在香港特别行政区，如果出现一个腐败的官员，那肯定是一 个惊天动地的大新闻，但是在我们中国大陆，如果出现一个清廉的官员，那肯定也是一个惊天动地的大新闻。前几年出了一个仁长霞，还拍成电影，叫每个人去看， 有什么好看的？美国发生过警察打黑人，校园出现过枪击案，公司发生过贿赂行为，路边发生过抢劫，政府里出现过腐败官员，每当此时，新华社就会抓住机会鼓足 干劲，大肆渲染，然后所有媒体倾巢而出，形成铺天盖地之势，不达家喻户晓誓不甘休。每当“帝国主义者”、“西方敌对势力”对中国大陆在人权、官员腐败、社 会治安上颇有微词时，我们便会反过来指责：你们也有，你们这些鬼佬，就是亡我之心不死，想要对我们进行和平演变。声讨之声也是所有媒体倾巢而出，形成铺天 盖地之势，不达全世界家喻户晓誓不甘休。这些一味指责别国干涉我国内政、转移国内人民的注意力的御用文人和党棍，你们不知道“盛世”的历史地位，不知道它 的历史意义，也不知道人间有羞耻这两个字，喋喋不休地说：我们现在是中国历史上最好的时代，是太平盛世，所谓“盛世中华”，迎“春晚”是也。<br />
<br />
如果硬要说“盛世”的话，那也只是既得利益者，官僚亲属子女、奸商、依附于既得利益集团的专家们的“盛世”，与十亿草民是无关的。对于中国十亿之众的民众实 际上是处于乱世之中，无论从政治、经济、文化、道德、信仰哪个层面上讲，都是在乱世之中，是迷茫、彷徨，在忧郁中艰难度日。这个不是说社会上的战乱动乱什 么的，现在世界上的主要国家，或者是大国，没有进行选举的，只有中国，不要说由主要大国组成的七大国家，也就是G7，后来加入我们变成G8（编者 注：1976年，加拿大加入G6成为G7，1997年，俄罗斯加入G7成为G8；大约在2003年，在法国和英国的提议下，五个发展中国家包括巴西、印 度、南非、墨西哥与中国应邀成为G8会议的客人，被称为G8+5；2008年，G20终于实质上取代了G5成为真正的世界经济论坛），全是民主国家，或者 后来扩大的G20，他们都是民主国家，也全部都实行了选举制度。<br />
<br />
幻灯片：“一个民主国家，主权应该在人民手中，这是天经地义的事：如果一个号称民主的国家，而主权不在人民手中，这决不是正轨，只能算是变态，就不是民主国家……不结束党治，不实行人民普选，如何能实现民主？把人民的权力交给人民？”<br />
<br />
大家看到这个，肯定会想，这肯定是西方反华寺里对我们的恶毒攻击，而事实上不是，以上的文字来源于1945年9月2日的《新华日报》，还有一条：“他们以为 中国实现民主政治，不是今天的事，而是若干年以后的事，他们希望中国人民知识与教育程度提高到欧美资产阶级民主国家那样，再来实现民主政治……然而正是在 民主制度之下才更容易教育和训练民众。”这是出自1939年2月25日《新华日报》的新闻。可以说中国共产党在60年前，就是一个穷小子，而新中国是一个 富家女，这位穷小子在迎娶富家女之前，对她许下了甜言蜜语，承诺了一大堆，而他们结婚以后，穷小子对富家女怎么样，现在的情况你们已经看到了。<br />
<br />
我不知道有多少中国人看到伊朗的选举会有感慨，会感到悲哀，因为连伊朗这样政教合一的国家都进行选举了，十三亿中国人至今还被剥削投票选举国家领导人的权 力。我们每个人都知道，我们下一届主席就是同志，习胖子，很胖，有点像《古惑仔》里的蒋天养，一看就像大反派。一个人不能精神分裂，一个国家也无需双轨 制，经济的多元化和政治的一元化本来就难以兼容的，原因在于政治的一元化会侵害法治、自由，威胁到每个公民的尊严和人身安全。中国不需要公仆，不需要人 民，我们都是公民，上至国家首脑，下至流浪汉。<br />
<br />
说了这么多，有人会觉得，你太无知 了，这个社会难道没有抚养过你吗？你有什么不满意的，难道你出门走的路都是免费的吗？国家对你做了这么多，你还对他提这么多要求！那我就觉得，持有这种想 法的人，你的追求是很低的，是国家最喜欢的公民品种，只要走路不用交钱你就满意了。那您还别开车，虽然中国路不多，但是世界上大多数收费公路都是在中国 的，连国道，都是收费的，虽然我们买了购置税、增值税、关税，每年的养路费、车船使用税，甚至某地的牌照钱….希望你出门的路不要被某老板看中，造了条高 速公路，这样你出门都要交钱。<br />
<br />
还有人说，你太无知了，你所接受的教育、住房、买东 西的商场、住的酒店、去看病的医院，都是国家给你的，你不知知恩图报反而在这里唧唧歪歪。我觉得抱有这种想法的人，明显是把福利和做生意给搞混了，上述的 那些，是做生意的事，义务教育到我所念书的时代还没有真正义务，现在的我不知道，不好乱说。<br />
<br />
上周刘老师问我：生活的意义是什么？我说：我不知道！因为我不知道我所热爱的生活什么时候可以到来。那是一种什么样的生活呢：人民可以自由的迁徙，不同的人 群可以和睦的相处，物价是很便宜的，房奴和学生是不受剥削的，公民可以独立自主地发表自己的见解。在《建国大业》里有这么一场戏：闻一多在一个公共广场进 行演讲，他说了这么一句话：你们这些搞一党专政的，不会有好下场，然后他晚上回去的时候，被人“突突”了。现在的话自然没人把你给“突突”了，但是你敢到 广场进行公开演讲吗？你不敢！！我热爱的生活，人民可以选出自己喜爱的人去操办自己的国家，房子是便宜的，车子是便宜的，油价也是便宜的，政府不会随意添 加无穷尽的税费，上网时没有防火墙，信仰是自由的。人们相互尊重，他们可以贫困，但是他们不仇富，他们的脸上没有戾气，永远不会像某些阶级斗争过的国家一 样，国民的脸上永远是警惕、杀气、猜疑、奸诈。他们信任规则，有生存的底线，有基础的道德，不会用最恶毒的手段，相互残杀，不会像某些国度那样，每个行业 都在尽可能地无耻，尽可能地相互坑害，并且嘲笑这个行业中不无耻的人是二百五。<br />
<br />
七十年的时间已经过去了，在历史的前夜你是不是也该扔掉烫手的权力山芋，从烤屁股的专政火炉上跳下来，还政于民。在历史的功劳簿上，包括决定的个人，留下光耀千古的宏伟一页！<br />
<br />
我跟大家说这么多并不是煽动大家造反，也不是号召大家起来革命，我只是想告诉大家，人不可以麻木，不可以只有立场，不分是非。悲哀的是在这个世界上太多的人 只有立场，不分是非，因为在这个国家，在这个国度，在公众场合站起来说几句心里话或者是真话是需要勇气的！谢谢大家！<br />
<br />
自由是一切生命体的权利!<br />
<br />
