　　一生裁为四截。前二十一年，算是入世前的准备：1939年出生重庆，在南京读小学、中学，在北京大学亲历反右运动；中间十八年，在边远地区贵州安顺小城中等专业学校教书，在社会底层经历了大饥荒和文化大革命；1978年重返北京，与北京大学青年学生、同代友人一起，风风雨雨二十四个春秋，并写有《心灵的探寻》、《与鲁迅相遇》、《周作人传》、《丰富的痛苦――堂吉诃德和哈姆雷特的东移》、《1948 ：天地玄黄》、《拒绝遗忘 ：一九五七年学研究笔记》等研究周氏兄弟和现代知识份子精神史的着作；2002年退休后五年，又回归中学和贵州，关注语文教育，西部农村教育，地方文化研究和青年志愿者运动，同时从事现代民间思想史研究。六十八年的生命，和两个空间――贵州与北京大学，一个群体――中国的年轻人，建立了血肉的联M，其主要联结纽带，则是鲁迅。<br />
