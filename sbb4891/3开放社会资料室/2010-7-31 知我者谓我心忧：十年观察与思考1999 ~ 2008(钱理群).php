书籍简介：<br />
这本书的最大特点就是“不公开性”，它从一开始就不准备公开发表，这样才保持了最大限度的思考与表达的真实性。我们这一代人活得实在太窝囊，一辈子都在看着别人的眼色，按照体制的要求，说大话，套话，废话，假话，做违心的事和文。现在老了老就太对不住自己了。我要最大限度保存一个真实的自我。<br />
<br />
本书的另一个重要动因，是要以此记念1989年的天安门民主运动二十周年。我始终认为，自己对二十年前，以青年学生、工人、市民的牺牲为代价的运动，是负有责任的。我说过：作为老师，却没有尽到保护学生的责任，尽管我无能为力，但我仍然不能摆脱内心的愧疚和不安，这至今仍是压在我心上的坟。谨以本书奉献给那场震撼世界的运动的?牲者和他们的亲人，以及因此而受到迫害的人们，作为我这个幸存者的心的纪念。<br />
