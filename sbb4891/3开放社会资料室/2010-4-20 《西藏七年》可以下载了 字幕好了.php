													<h2>阿沛・阿旺晋美谈《在西藏七年》及其他</h2>
						
						
																											<div id="postmessage_648500" class="t_msgfont">【编者按：1998年3月23日上午9时30分至11时30分，全国政协副主席阿沛・阿旺晋美接受了《南华早报》驻京记者白克尔先生的采访，就美国电影《在西藏七年》以及其他内容广泛的问题发表了谈话，现将这次谈话内容刊载于下】 <br />
　　 <br />
　　　　白克尔：（以下简称白）您在北京居住多少年了？ <br />
　　 <br />
　　　　阿沛・阿旺晋美副主席（以下简称阿）1991年之后，我就没有再去过西藏了。在此之前，我有时住在北京，有时住在西藏，因为我在西藏有工作，1967年在北京安的家。 <br />
　　 <br />
　　　　白：您在昌都有家吗？ <br />
　　 <br />
　　　　阿：没有，在昌都工作时住的是政府提供的房子，没有私人住宅。 <br />
　　 <br />
　　　　白：您曾经是昌都的王吗？ <br />
　　 <br />
　　　　阿：不是王，在一段时间曾经是西藏地方政府驻昌都总管。 <br />
　　 <br />
　　　　白：您年轻时一直住在拉萨吗？住了多少年？ <br />
　　 <br />
　　　　阿：我一直住在拉萨，我生在拉萨，长在拉萨，工作在拉萨。 <br />
　　 <br />
　　　　白：您在拉萨见过海因利希・哈勒（《在西藏七年》一书的作者）吗？ <br />
　　 <br />
　　　　阿：见过一两次。初次见面是在1948年藏历五月十五日，这一天是西藏民间一个很重要的节日，叫做“瞻部林吉桑”，当时我们在公园举办朋友聚会，雪康・土登尼玛带他来，介绍我们认识，这以后他到过我家一两次。 <br />
　　 <br />
　　　　白：您对他的印象如何？ <br />
　　 <br />
　　　　阿：我们见面时只谈了一些琐事，未深谈，也未涉及政治问题，他这个人很爱玩，爱打麻将。 <br />
　　 <br />
　　　　白：他在拉萨有什么样的名气？ <br />
　　 <br />
　　　　阿：哈勒在拉萨没有多大的名气。他有个朋友叫奥夫斯内特，懂得建筑，为拉萨办几件实事，比如说，修水渠，将拉萨河上游的水引到罗布林卡；在拉萨市里修下水道；当时要在纳金修建小电厂，已修起了一些职工住房，他进行了勘测、设计，但没有建成。因此，在拉萨知道他的人很多。哈勒是个到处游荡的人，没办什么实事，也没有显示出有多大的能耐，没什么名气，影响也不大。 <br />
　　 <br />
　　　　白：哈勒写了一本书《在西藏七年》，您是否读过？ <br />
　　 <br />
　　　　阿：我听说过，没看过。 <br />
　　 <br />
　　　　白：哈勒离开西藏后您再没有见过他吗？ <br />
　　 <br />
　　　　阿：没有。他是1951年3、4月间离开拉萨的，那时我不在拉萨，在北京进行和平协议的谈判工作。我1951年8、9月间回到拉萨，他已经走了，从此再未见过他。 <br />
　　 <br />
　　　　白：您看过美国电影《在西藏七年》吗？ <br />
　　 <br />
　　　　阿：我最近才看到。白：有什么印象、看法？阿：除了有的小问题可能接近事实，例如影片中有教达赖喇嘛学习英语的情节，其余全部都不符合事实。 <br />
　　 <br />
　　　　白：影片中有个叫阿旺的，好象是演的您？ <br />
　　 <br />
　　　　阿：我可以这样说，完全是捏造。影片中有个情节，描述我派一位妇女送藏袍给哈勒，根本就没有这回事，我从来没有想到送衣服给他，我仅仅是认识他，他只是到过我家一两次。 <br />
　　 <br />
　　　　白：影片给观众的印象您不是一个爱国者，而是卖国者，影片特别批评了您做的事情，对此您有什么看法？ <br />
　　 <br />
　　　　阿：我看了之后认为，哈勒本人不可能在书中写下这样的情节，这是电影的炮制者编造的。我和哈勒没有那样的交往和关系，所以他不可能在书中写这样的东西，尽管我没看过这本书。这部片子是在美国拍摄的，我认为这是美国电影作者编造的。 <br />
　　 <br />
　　　　白：我注意到了，很多事情书中没有，电影中有，二者不一样，有很大的区别。1951年达赖喇嘛和西藏政府要您领导西藏军抵抗解放军进入昌都，您没完成您的任务，电影就此批评了您，您对此有什么看法？ <br />
　　 <br />
　　　　阿：我可以详细地介绍这段历史的情况。西藏地方政府有个规定，凡西藏地方政府的官员、贵族在他为政府服务期间必须去昌都工作，任期4年。1950年初，西藏地方政府委派我去接任昌都总管，我明确提出不去，因为我20多岁时就在昌都的总管府做过粮官，服务了4年，我的任务已经完成。第二个原因，西藏当时还有个规定，贵族家庭如果没有了长辈，而本人又是家中的长者，就可以不去昌都，我正好符合这个规定。当时解放军已临近昌都，西藏地方政府正与中央政府进行和平谈判，我说我不做昌都总管，如果要进行和平谈判我可以去。我提出这个意见后，西藏地方政府就召开了各级官员代表会议讨论，他们认为我以噶伦的身份去进行和平谈判，规格太高，下一轮需要噶伦出面，你再去。他们要我必须去昌都任总管，根据这个决定我只好去上任。我到那里时情况很糟，很危险。已有6个团3千多人的藏军调到前线，还动员了1万多民兵。而军粮、生活用品全都要老百姓运输，当时西藏的交通十分落后，要先将各地的物资集中起来，再运到前线，老百姓苦不堪言，很多人都断了粮，大家怨声载道。在这种情况下，不要说同解放军打仗，恐怕要发生内乱，形势很严重。我当时着手遣散了5、6个县的民兵，这当口昌都解放了。 <br />
　　 <br />
　　　　白：很多康巴战士是不是想和解放军打仗？并作了准备？ <br />
　　 <br />
　　　　阿：不存在这样的事，这是乱说的。 <br />
　　 <br />
　　　　白：解放后康巴战士和义民进行了多次起义，反抗土地改革，是吗？ <br />
　　 <br />
　　　　阿：情况不是这样的。西藏和平解放之后，是按照和平协议进行工作的。1956年，在青海甘肃、云南、四川藏族聚集区进行了民主改革，引起了一些人的反对，搞了叛乱，叛乱平息后这些人跑到了西藏，有人就认为是西藏搞了叛乱。 <br />
　　 <br />
　　　　白：西藏解放之前您和解放军有联系吗？ <br />
　　 <br />
　　　　阿：解放军到西藏之前我与他们没有任何联系，见都没见过。 <br />
　　 <br />
　　　　白：您了解共产主义吗？ <br />
　　 <br />
　　　　阿：西藏解放之前我和共产党没有任何联系，也不懂共产主义，但通过对西藏历史的研究我很清楚，西藏是中国领土不可分割的一部分，西藏在中国中央政权管辖下已有700多年的历史。1911年中国内地发生了辛亥革命，推翻了清王朝的中央政府，国民党掌握了政权，在这一过程中，对西藏的管理有些顾不上，不是那么紧密、严格，在形式上有脱离中国的现象，但实际上西藏从未实现过独立，对于这一点我心里有数，很清楚。另一点是，我认为不能与共产党打仗，只能和谈。国民党号称有800万军队，有美国的支持，与共产党打仗的结果还是失败。如果西藏男女老幼全都上阵打仗，那时西藏不足100万人口，没经过训练，没有武器，怎么和解放军打？怎么取得胜利？我一直主张和谈。这些主张，在当时噶厦召开的官员会议上讨论打还是和谈问题时，我几次明确讲过。这并不是我对共产党有所了解，或是和解放军有什么联系，而是从西藏的历史和现实出发考虑的。 <br />
　　　　白：您是如何理解共产党对西藏的政策？比如对土地改革、佛教、和尚、西藏的传统等。 <br />
　　 <br />
　　　　阿：1949年，中华人民共和国成立后，当时在拉萨有很多谣言，说共产党杀和尚、烧寺庙、抢财产、强奸妇女，甚至吃人肉。我个人认为非常明显，这是外国人造谣。但确有不少人相信，他们想共产党既然如此，就没有了活头，还不如拼个你死我活。对于是和谈还是打仗，我当时谈了我的意见，国民党时期中国有4亿人口，中国共产党掌管了政权之后，这些人都能活下去，我们西藏不到100万人口，有什么不能活下去？如果共产党确如谣言所说，那4亿中国人也不会老老实实地受共产党的欺负吧？我的话有人认为有道理，有的人听信谣言，还是要与共产党打仗。 <br />
　　 <br />
　　　　白：您加入共产党吗？ <br />
　　 <br />
　　　　阿：没有。 <br />
　　 <br />
　　　　白：最近在英国有一篇关于十世班禅“七万言书”的报道，您看过了吗？ <br />
　　 <br />
　　　　阿：这件事我清楚，十世班禅写“七万言书”时征求过我的意见，我们讨论过。我劝他你对西藏工作有什么意见可以口头提出，最好不要写成书面的，这样会当成把你打倒的一个根据。我很坦率地提出了，他不听。“七万言书”的内容我看了。 <br />
　　 <br />
　　　　白：十世班禅的“七万言书”中谈到共产党毁了很多寺庙，杀了很多和尚，有很多人饿死，他害怕藏民族消亡，文化失传，他说得很严重，您觉得他说的有道理吗？3年困难时期您在干什么？ <br />
　　 <br />
　　　　阿：西藏在1959年发生叛乱，之后进行了民主改革、土地改革、发展生产，总的来看是好的，中央对西藏的政策是正确的、一贯的，但西藏干部的水平不一样，对中央政策的理解程度不一样，这就出现了差别，有的地方政策执行得好，有的地方执行得不好。班禅大师看到了个别地方，未从全局看这些问题。他的“七万言书”有对的地方，也有不对的地方。 <br />
　　 <br />
　　　　白：您估计当时有多少人饿死或死于监狱？ <br />
　　 <br />
　　　　阿：我可以肯定地讲，当时在西藏没有饿死一个人。但听说青海有人饿死，至于多少人，我没去过青海，无法统计。班禅大师讲青海的情况是这样的，那时我们都在北京开会，之后我和帕巴拉・格列郎杰去东北参观，班禅大师去青海，走之前还没有什么问题，但他从青海了解情况回来后很生气，向西南局作了汇报，之后他在返回北京的路上开始写“七万言书”。 <br />
　　 <br />
　　　　白：据说他因此而受到批评和斗争，入狱15年？ <br />
　　 <br />
　　　　阿：在他写了“七万言书”之后，对他进行了严厉的批评，他担任的西藏自治区筹备委员会主任职务被撤销，送到北京，保留了全国政协委员，长住北京。1967年，“文化大革命”期间，中央民族学院的一批红卫兵翻墙到班禅大师家，把他抓到民族学院进行批斗。周恩来总理知道此事后马上派秘书童小鹏将他接了出来。当时情况很乱，无法无天，周总理把他送到一个地方保护起来，那里也保护着许多共产党的高级干部、部队的高级将领。这件事从头到尾我很清楚。 <br />
　　 <br />
　　　　白：十世班禅说您参与了六十年代初批评他的事，对此他一直耿耿于怀。你们的关系不太好是吗？ <br />
　　 <br />
　　　　阿：我没有批评过班禅大师，原因很简单，我没有材料和根据。1964年，在西藏自治区筹备委员会第七次会议上作出决议，撤销他西藏自治区筹备委员会代主任的职务，由我担任这个职务。征求我的意见时，我明确地说他有错误，可在内部进行批评，甚至是严厉的批评，但不能撤销他的职务，改由我去担任，我不同意。我很明确地对当时的负责人表达了我的意见。当时他是被批评的对象，这些事他不可能知道。后来我们都当了全国人大常委会的副委员长，在一次会上，我当着许多人，将这件事向他讲了。我们一直保持着友好的关系。我没批评过他，他也没批评过我，你们听到的可能是谣言。 <br />
　　 <br />
　　　　白：80年代，十世班禅多次提意见，说1979年以前西藏的宗教政策没有搞好。有时他在公开场合发表的意见与他内心的想法不一致。他说提意见是一件很痛苦的事情，您同意他的看法吗？ <br />
　　 <br />
　　　　阿：这些方面肯定有不同的看法，有时确实班禅大师讲了错话，讲的很厉害，在现场当着他的面不好讲他说错了话，会后我们内部个别交谈时，我都会指出来哪些话说错了，有时他也承认自己错了。我们无论谁到谁家，都会明确提出意见，我们之间没有矛盾。他常说，他说错了什么，做错了什么都要我告诉他，反之我也一样。 <br />
　　 <br />
　　　　白：80年代班禅活着的时候，中国政府的政策是和达赖谈判，并通过班禅和达赖的亲戚好像有过接触，班禅死了以后，这一过程好像中断了。您怎么看？ <br />
　　 <br />
　　　　阿：您刚才的说法是错误的，中央对达赖的政策是一贯的，谈判的条件只有一个，就是达赖放弃西藏独立的主张，停止进行分裂祖国的活动。过去一直有过接触，当然还不是正式谈判。后来这种接触、联系中断了，是达赖中断的，不是中央中断的，而且这与班禅大师在世、去世没有任何关系，这点我特别清楚。班禅大师去世后，中央对他的后事处理十分重视，要在北京举行隆重的追悼大会。当时我们向中央建议，这是与达赖接触的好机会，可以以适当的方式邀请达赖参加班禅大师的追悼大会，他就此可与中央政府进行接触，中央接受了这个意见，商量由中国佛教协会的会长赵朴初居士出面，写了一封亲笔信给达赖喇嘛，邀请他出席班禅大师的追悼会。当时达赖的二哥嘉乐顿珠就在北京，他到我家做客，我向他谈及此事，讲明这是达赖和中央政府亲自接触的难得的好机会。请他转告达赖，我希望他务必来，不要错过这个机会。嘉乐顿珠也认为是个好机会，表示要亲自带信到印度当面交给达赖，并说服达赖一定来北京。后来这封信由嘉乐顿珠亲自面交了达赖，但达赖拒绝了。这方面的接触是达赖中断的，而不是中央中央政府中断的。这和班禅大师的去世没有关系。 <br />
　　 <br />
　　　　白：有件事我不太了解，就是恰扎与达赖换信被抓的情况。 <br />
　　 <br />
　　　　阿：这件事详细谈会很长，简单介绍达赖和班禅转世灵童认定问题。从八世达赖时期，清朝中央政府确定了金瓶掣签制度，从九世达赖开始实行，用这种办法来确定灵童转世的正身。金瓶掣签之前，按规定要选出几位候选灵童。认定达赖的转世灵童可由在世班禅打卦，挑选几名候选者。认定班禅的灵童，可由在世的达赖打卦认定几名候选者。如果达赖和班禅都不在世，可由一位有威望的大活佛打卦认定候选灵童。这种认定仅仅是候选灵童，而不是灵童的正身。正身必须在大昭寺的释迦牟尼像前进行金瓶掣签认定，最后报请中央政府批准。在认定十世班禅灵童的问题上，我对中央政府提出过建议，为了将这件事办得圆满，可由扎什伦布寺的民主管理委员会出面给达赖写信，让达赖打卦认定一个候选灵童，这个灵童可与国内其他候选灵童一起进行金瓶掣签。后来达赖突然宣布，就认定他候选的那个小孩为班禅大师转世灵童的正身。这根本就不符合规矩，不要说达赖在国外，就是在国内也无权最后认定灵童。这件事是达赖和恰扎私下里做的，恰扎要求达赖这样做，达赖也特别愿意这么干，这完全违背了传统的做法，恰扎当然就被关起来了。 <br />
　　 <br />
　　　　白：恰扎现在在哪理？ <br />
　　 <br />
　　　　阿：肯定关起来了，在哪里我不知道。白：他犯了什么罪？按法律是什么罪名？阿：这件事恰扎有很严重的错误。如果你写信给达赖，请他选出一位候选灵童，那我看就没有问题。而恰扎不是这么做的，他报过一个名单去，说就请你认定这个小孩为转世灵童正身，这就违反了定制。这样做法在法律上定什么罪，我不清楚。但这是一个很严重的问题。白：达赖选的灵童在哪里？阿：在一个地方学习。 <br />
　　 <br />
　　　　白：从长远看，您对与达赖的谈判有什么看法？为什么不继续这种谈判？为何要中断这种接触？ <br />
　　 <br />
　　　　阿：这个问题刚才讲得很清楚了，是达赖中断接触的，不是中央政府停止接触的。 <br />
　　 <br />
　　　　白：那您为何不到印度与达赖谈谈？你们彼此都认识。 <br />
　　 <br />
　　　　阿：在这个问题上我出了很大的力量，但是毫无结果。何必再费心呢。请你考虑一个问题，《在西藏七年》这部电影从头到尾都是捏造，如果不是达赖他们出的主意，怎么会有这个东西。在这种情况下与达赖谈什么呢？这部电影对我进行了恶毒的攻击，我不在乎。毛泽东有个观点，被敌人反对是件好事，凡是敌人反对的我们就要拥护，越骂我，我越高兴。 <br />
　　 <br />
　　　　白：现在班禅很年轻，达赖已经老了，他死了以后，西藏人民怎么办？ <br />
　　 <br />
　　　　阿：这一点请你放心，西藏的社会已经发生了变化，西藏人民的生活越来越好。这一点是肯定的。</div>

							
							
							
							
