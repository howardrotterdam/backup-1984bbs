(博讯北京时间2009年12月25日 转载)<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;针对阿沛阿旺晋美去世，西藏流亡政府今天（2009年12月24日）从达然萨拉发表声明表示哀悼。声明全文如下： (博讯 boxun.com)<br />
