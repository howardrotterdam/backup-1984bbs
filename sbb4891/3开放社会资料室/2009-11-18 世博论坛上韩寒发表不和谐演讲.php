																											<div id="postmessage_333177" class="t_msgfont">韩寒：<br />
大家好！<br />
我很少来这样的场合，我比较习惯回答问题，我觉得在上面讲总有一种想要说服人的感觉。只参加过一次，当时区里面请一些在外面混得比较好的人回来，我有幸是比较好的人，当时讲了高速公路修的多方便。论到我讲的时候还有点紧张，讲了两个问题。这次是嘉定区两位朋友请我来的，还有在位的几位老师，都是我比较敬佩的人。我也准备了一点东西，主要也是为了约束自己。<br />
讲的主题是，城市让生活更糟糕，与世博会的口号有一点出入。我一个同学，邀请我一起吃饭，我的朋友当一个很小的职位，当时说“城市、让生活更美好”不够好，应该是“城市，让美好的生活更美好”。<br />
不知道从那一年开始，上海要给每一个区定一个方向，金山区被并成了了化工业，嘉定很幸运，松江发展的也很多，闵行区、南汇区渔业发展比较好。嘉定区我每年都要来几次，这里面有F1的赛车场。虽然建得太豪华一些，当时应该是最豪华的赛车场。后来中东建了两个赛车场，把嘉定的赛车场比下去了。<br />
最近油价又涨了，我有些朋友用过以后，反映油启动起来很困难，车辆有点发抖，油耗非常大。发改委跟法院一样是服务于政府的。不知道发改委打飞机的笑话大家听过没有。有人发现，只要发改委一涨价，就要坠一个飞机，有美国的、伊朗的等等。我恳求在和平的年代，中国政府不要随意使用发改委这个武器。<br />
油价最好一步到位比较好。在短期内，一两年内不会有什么影响，不久的将来油价应该在10块钱一升，中级的目标再加上通货膨胀应该在20块钱一升，应该是全世界油价最贵的一家了。<br />
涨到20块钱一升，政府是有理由的，比如是为了促进环保，因为大家都喜欢用环保这个词，很多人对环保的概念非常凸显。拿汽车来说，开小排量的车是环保的，这个理念在中国不成立。假设是300万的大排量的汽车，交税应该有30万左右，如果像增收的目的一样，用于环保事业，这些大排量的车主对环保事业的贡献是很大的。<br />
我现在在做杂志，我的杂志社在浦东，离开我住的地方大概20公里，每次开过去要花2个小时，平均时速是每小时13公里，我住的地方要通过一个地道，地道是闵行区，闵行区没有丝毫要拓宽的意思。有一天沪杭高速修路，那时我请朋友吃饭，我从家里6点钟出来，到一家必胜客去，但是到哪里已经关门了。于是我就买了自行车、电动车。<br />
我是一个非常不喜欢大城市的人，我生来就是一个乡下人，从小在向下长大，一直到初中时候还是农村户口，父亲和母亲都是农村户后，父亲是文革时候考取华东示范大学，后来因为肝炎退学了。退学以后自学，但是还是拿到了大学文凭。父亲当时在农村的文化站工作，我也受了他很多影响。但是父亲开车不行，还好这点没有受他的影响。<br />
我的母亲非常焦虑，因为我的户口是农村户口，我不知道户口有很重要的意义。好在现在户口不是那么重要的，只要有套房子就可以了，这也是社会的进步。当时我也不在乎户口的问题，妈妈担心我要谈恋爱，其实当时我已经谈恋爱了，但是那个时候的恋爱比较单纯，女朋友从来不问家里房子有多大。当时我们互相留了电话，那个时候装电话是很激动的事。有的时候通过电话聊天，我觉得我家的电话哪儿都好，就是声音就点空，后来已经爸妈也在楼上听。<br />
在龙州的时候，当地要发展经济会想一些办法，当时有一个龙游石窟，后来才知道是龙门石窟。有一个肯德基会觉得很温暖，而不是像上海有那么多的中心，我对这些是没有感觉的，跟我是没有关系的。<br />
我当时在北京住望京，后来到了朝阳区，是北京最好的区。我不知道为什么，可能是命中注定，住的地方不是特别合适，但始终在郊区。在上海以前在金山，后来在松江。可能是天生更喜欢郊区，有蓝蓝的白云天，但是向下很多不是这个白云天了。<br />
我觉得城市是实现年轻人梦想的地方，尤其是上海。经常有人问我中国会不会出现垮掉。我说不会，如果中国要出现怎么样的一代，首先是房地产业先垮掉，然后年轻人才会有理想。年轻人的理念有很大的转变，旅行是大部分年轻人的理想。80年代有很多年轻人去流浪，现在如果有人去流浪一定是认为不正常的。歌词中经常有让心去流浪，这意味着人还在城市里面。 <br />
北京一个国土司的领导讲，大量的人涌入城市，从房价长远来看，还是要涨价的，但是会通过税收的方式将涨价的这部分返还给民众。这个说法是比较新颖的。说房子没有特别大的意义，因为不被大家说的很烂了。上海的公寓是203万一套，在外环线之内，没有1万块钱的房子里。我觉得我们没有必要拿房价和国外资本主义国家对比，因为我们走的是中国特色社会主义道路。<br />
那个司长说的话，好比汽油涨了20块钱一升，再多加5块钱的税，这个税用于经济的发展。这个提法是非常新颖的，无论是物业税对房价的影响。我认为房价的多少完全是政府的意思，政府已经维护证券的稳定，要让足够多的公务员和官员得到好处，行政的成本是非常大的。政府的收入主要靠两块，一块是土地，一块是税收。<br />
地都卖完怎么办？就是所谓推进城市化的进展，高房价对政府来说有很多的好处，一个是自己赚得比较，第二是朋友赚得比较多，因为房地产和政府有关系。房奴还有一套房子，所以他们最害怕失去。高房价对执政地位有帮助，所以房价不会低的。我并不奢望政府有什么举措，让老百姓用5年或者10的收入买到房子。<br />
我最近发现，老是攒我下钱来，虽然我的收入还算不错。因为身边的朋友都要到要买房子的时候，很多时候缺钱会想到我，其实我住的房子也很破。正是因为，我才在抱怨社会，我觉得太贵了。他们的问题是，如何将生活过下去，尤其在上海，除非你是无产阶级高官的儿子。<br />
中国的大城市就是这样，毁灭100万个理想，用这100万个理想诞生出1到2个新富。事实上，我个人很不喜欢这样，我的同学无论是生活还是恋爱都非常现实。这两年大学扩招了，我并不奢望大学扩招能多培养人才，我觉得可以稀释大学生很脑残的萌动，从学校出来的学生是很可怜的。现在的大学生真的很幸福，因为他们出来的时候并不是忙自己的事业，更多的是被房子所困扰。一方面房子替代了户口，一方面上海这个城市不能给人安全感。在这个地方生活下来不是很容易，随着世博会的临近我们的生活成本越来越贵，出租车是13块钱起步，地铁是3块钱起步。<br />
我认为上海不是一个真正大都市，因为我觉得一个真正的大都市是可以安居乐业的。上海政府把上海当成冒险家的乐园，保险家的乐园虽然好听，但是我不赞成。我觉得这样的城市并不适合居民居住，这也是把为人民服务当成养猪做。<br />
我体这些建设性的意见，在座的各位，包括新闻媒体的从业者，包括以前的领导，都怀有自己的理想，来到自己的工作岗位中，到最后都变成了迫于生计。任何一个精神独立，作品带有批判色彩的人，对自己的行业肯定少不了建设性建议意见。事实上，在大家少年不懂事的事物，也提过所谓建设性的意见。最后你会发现提建设性的意见是很痛苦的事情，因为没有人需要你建设性的意见。短期内还是不要做一个建设者。<br />
当时跟陈老师说的时候，在北京有一个对话，说起故乡的概念。我发现现在农民未必那么热爱自己的土地和故乡。我回去的时候，那里有一个老房子，我觉得到哪里就像到了根一样。有一次听说要拆迁，当时我不同意，坚决不能拆我的房子，给我多少前也没有用，我爷爷也很着急，想想有没有办法不把这个房子拆掉。<br />
我是一个很可怕的税民，我有一个希望，有一天我不小心死掉了，我希望政府能免费捞我的尸体。爷爷说能不能搞一个故居，我说爷爷你把我当余秋雨了，不行。那时候那里搞了一个物流城黄了，还有一个雕塑园黄了。最后那块地有幸得以生存下去，最后这个房子还留在那里，没有人拆，短期内也不会有什么威胁。<br />
我就很高兴告诉邻居这个房子没有拆掉。但是邻居不一定那么高兴。我邻居的邻居在上海上学，我发现他跟我不一样的是，他可能相中了上海这个地方，看不起以前的那个土地，希望和以前所有的土地关系决裂。好歹在镇上买一套公寓，两三代人住在一起，他们也愿意。当然每个人都有每个人的想法，现在的女孩子出去嫁人的时候，特别看中房子。有的时候男性改变世界，女性改变男性的世界观，真的是没有办法。但是很多人不喜欢自己的故乡。如果连故乡都没有的话，心里并不是特别好的滋味，这样一个城市是没有腔调的。<br />
上海是我的故乡，很多地方请我去，有的时候送我别墅。我说不是从上海出来的，这里是我的故乡，我希望我的故乡可以让每个来这里的年轻人有理想、有抱负，有一个自己容身的地方。<br />
…… 最后期望世博早日的开幕，如果是城市让生活更痛苦的话，我就希望它早日的闭幕。<br />
中华民族从哪里来，到哪里去，脉络是清晰的，哪怕是在战火不绝的军阀时代、日本入侵时代，泱泱民族也知道自己的位置，明白未来的方向，那就是最终要归入人类主流文明，与世界文明接轨<br />
<br />
中国政治现代化虽屡经挫折，但追求民主的线索始终没有断过，即便是在抗战期间的重庆，只有到了毛这里，通过一系列思想改造运动，然后到50年前发生的铺天盖地的反右运动，将此前接受过健康文明教育的知识分子，呼吸过欧风美雨，对法治、人权、自由、民主等普世价值有着清晰认知的那一部分人，几乎全都打入了十八层地狱，这些人大多数未能熬过寒冬，活着走出“文革”，在身体上消失了。他们的离开，放在整个现代化史上，意味着上百年来本民族政治现代化的一点微弱积累归零。</div>

							
							
							
							
