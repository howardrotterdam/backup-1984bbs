													<h2>世博论坛韩寒，陈丹青等讲话全文</h2>
						
						
																											<div id="postmessage_333345" class="t_msgfont">韩寒：<br />
&nbsp; &nbsp; 大家好！<br />
&nbsp; &nbsp; 我很少来这样的场合，我比较习惯回答问题，我觉得在上面讲总有一种想要说服人的感觉。只参加过一次，当时区里面请一些在外面混得比较好的人回来，我有幸是比较好的人，当时讲了高速公路修的多方便。论到我讲的时候还有点紧张，讲了两个问题。这次是嘉定区两位朋友请我来的，还有在位的几位老师，都是我比较敬佩的人。我也准备了一点东西，主要也是为了约束自己。<br />
&nbsp; &nbsp; 讲的主题是，城市让生活更糟糕，与世博会的口号有一点出入。我一个同学，邀请我一起吃饭，我的朋友当一个很小的职位，当时说“城市、让生活更美好”不够好，应该是“城市，让美好的生活更美好”。<br />
&nbsp; &nbsp; 不知道从那一年开始，上海要给每一个区定一个方向，金山区被并成了了化工业，嘉定很幸运，松江发展的也很多，闵行区、南汇区渔业发展比较好。嘉定区我每年都要来几次，这里面有F1的赛车场。虽然建得太豪华一些，当时应该是最豪华的赛车场。后来中东建了两个赛车场，把嘉定的赛车场比下去了。<br />
&nbsp; &nbsp; 最近油价又涨了，我有些朋友用过以后，反映油启动起来很困难，车辆有点发抖，油耗非常大。发改委跟法院一样是服务于政府的。不知道发改委打飞机的笑话大家听过没有。有人发现，只要发改委一涨价，就要坠一个飞机，有美国的、伊朗的等等。我恳求在和平的年代，中国政府不要随意使用发改委这个武器。<br />
&nbsp; &nbsp; 油价最好一步到位比较好。在短期内，一两年内不会有什么影响，不久的将来油价应该在10块钱一升，中级的目标再加上通货膨胀应该在20块钱一升，应该是全世界油价最贵的一家了。<br />
&nbsp; &nbsp; 涨到20块钱一升，政府是有理由的，比如是为了促进环保，因为大家都喜欢用环保这个词，很多人对环保的概念非常凸显。拿汽车来说，开小排量的车是环保的，这个理念在中国不成立。假设是300万的大排量的汽车，交税应该有30万左右，如果像增收的目的一样，用于环保事业，这些大排量的车主对环保事业的贡献是很大的。<br />
&nbsp; &nbsp; 我现在在做杂志，我的杂志社在浦东，离开我住的地方大概20公里，每次开过去要花2个小时，平均时速是每小时13公里，我住的地方要通过一个地道，地道是闵行区，闵行区没有丝毫要拓宽的意思。有一天沪杭高速修路，那时我请朋友吃饭，我从家里6点钟出来，到一家必胜客去，但是到哪里已经关门了。于是我就买了自行车、电动车。<br />
&nbsp; &nbsp; 我是一个非常不喜欢大城市的人，我生来就是一个乡下人，从小在向下长大，一直到初中时候还是农村户口，父亲和母亲都是农村户后，父亲是文革时候考取华东示范大学，后来因为肝炎退学了。退学以后自学，但是还是拿到了大学文凭。父亲当时在农村的文化站工作，我也受了他很多影响。但是父亲开车不行，还好这点没有受他的影响。<br />
&nbsp; &nbsp; 我的母亲非常焦虑，因为我的户口是农村户口，我不知道户口有很重要的意义。好在现在户口不是那么重要的，只要有套房子就可以了，这也是社会的进步。当时我也不在乎户口的问题，妈妈担心我要谈恋爱，其实当时我已经谈恋爱了，但是那个时候的恋爱比较单纯，女朋友从来不问家里房子有多大。当时我们互相留了电话，那个时候装电话是很激动的事。有的时候通过电话聊天，我觉得我家的电话哪儿都好，就是声音就点空，后来已经爸妈也在楼上听。<br />
&nbsp; &nbsp; 在龙州的时候，当地要发展经济会想一些办法，当时有一个龙游石窟，后来才知道是龙门石窟。有一个肯德基会觉得很温暖，而不是像上海有那么多的中心，我对这些是没有感觉的，跟我是没有关系的。<br />
&nbsp; &nbsp; 我当时在北京住望京，后来到了朝阳区，是北京最好的区。我不知道为什么，可能是命中注定，住的地方不是特别合适，但始终在郊区。在上海以前在金山，后来在松江。可能是天生更喜欢郊区，有蓝蓝的白云天，但是向下很多不是这个白云天了。<br />
&nbsp; &nbsp; 我觉得城市是实现年轻人梦想的地方，尤其是上海。经常有人问我中国会不会出现垮掉。我说不会，如果中国要出现怎么样的一代，首先是房地产业先垮掉，然后年轻人才会有理想。年轻人的理念有很大的转变，旅行是大部分年轻人的理想。80年代有很多年轻人去流浪，现在如果有人去流浪一定是认为不正常的。歌词中经常有让心去流浪，这意味着人还在城市里面。&nbsp; &nbsp; <br />
&nbsp; &nbsp; 北京一个国土司的领导讲，大量的人涌入城市，从房价长远来看，还是要涨价的，但是会通过税收的方式将涨价的这部分返还给民众。这个说法是比较新颖的。说房子没有特别大的意义，因为不被大家说的很烂了。上海的公寓是203万一套，在外环线之内，没有1万块钱的房子里。我觉得我们没有必要拿房价和国外资本主义国家对比，因为我们走的是中国特色社会主义道路。<br />
&nbsp; &nbsp; 那个司长说的话，好比汽油涨了20块钱一升，再多加5块钱的税，这个税用于经济的发展。这个提法是非常新颖的，无论是物业税对房价的影响。我认为房价的多少完全是政府的意思，政府已经维护证券的稳定，要让足够多的公务员和官员得到好处，行政的成本是非常大的。政府的收入主要靠两块，一块是土地，一块是税收。<br />
&nbsp; &nbsp; 地都卖完怎么办？就是所谓推进城市化的进展，高房价对政府来说有很多的好处，一个是自己赚得比较，第二是朋友赚得比较多，因为房地产和政府有关系。房奴还有一套房子，所以他们最害怕失去。高房价对执政地位有帮助，所以房价不会低的。我并不奢望政府有什么举措，让老百姓用5年或者10的收入买到房子。<br />
&nbsp; &nbsp; 我最近发现，老是攒我下钱来，虽然我的收入还算不错。因为身边的朋友都要到要买房子的时候，很多时候缺钱会想到我，其实我住的房子也很破。正是因为，我才在抱怨社会，我觉得太贵了。他们的问题是，如何将生活过下去，尤其在上海，除非你是无产阶级高官的儿子。<br />
&nbsp; &nbsp; 中国的大城市就是这样，毁灭100万个理想，用这100万个理想诞生出1到2个新富。事实上，我个人很不喜欢这样，我的同学无论是生活还是恋爱都非常现实。这两年大学扩招了，我并不奢望大学扩招能多培养人才，我觉得可以稀释大学生很脑残的萌动，从学校出来的学生是很可怜的。现在的大学生真的很幸福，因为他们出来的时候并不是忙自己的事业，更多的是被房子所困扰。一方面房子替代了户口，一方面上海这个城市不能给人安全感。在这个地方生活下来不是很容易，随着世博会的临近我们的生活成本越来越贵，出租车是13块钱起步，地铁是3块钱起步。<br />
&nbsp; &nbsp; 我认为上海不是一个真正大都市，因为我觉得一个真正的大都市是可以安居乐业的。上海政府把上海当成冒险家的乐园，保险家的乐园虽然好听，但是我不赞成。我觉得这样的城市并不适合居民居住，这也是把为人民服务当成养猪做。<br />
&nbsp; &nbsp; 我体这些建设性的意见，在座的各位，包括新闻媒体的从业者，包括以前的领导，都怀有自己的理想，来到自己的工作岗位中，到最后都变成了迫于生计。任何一个精神独立，作品带有批判色彩的人，对自己的行业肯定少不了建设性建议意见。事实上，在大家少年不懂事的事物，也提过所谓建设性的意见。最后你会发现提建设性的意见是很痛苦的事情，因为没有人需要你建设性的意见。短期内还是不要做一个建设者。<br />
&nbsp; &nbsp; 当时跟陈老师说的时候，在北京有一个对话，说起故乡的概念。我发现现在农民未必那么热爱自己的土地和故乡。我回去的时候，那里有一个老房子，我觉得到哪里就像到了根一样。有一次听说要拆迁，当时我不同意，坚决不能拆我的房子，给我多少前也没有用，我爷爷也很着急，想想有没有办法不把这个房子拆掉。<br />
&nbsp; &nbsp; 我是一个很可怕的税民，我有一个希望，有一天我不小心死掉了，我希望政府能免费捞我的尸体。爷爷说能不能搞一个故居，我说爷爷你把我当余秋雨了，不行。那时候那里搞了一个物流城黄了，还有一个雕塑园黄了。最后那块地有幸得以生存下去，最后这个房子还留在那里，没有人拆，短期内也不会有什么威胁。<br />
&nbsp; &nbsp; 我就很高兴告诉邻居这个房子没有拆掉。但是邻居不一定那么高兴。我邻居的邻居在上海上学，我发现他跟我不一样的是，他可能相中了上海这个地方，看不起以前的那个土地，希望和以前所有的土地关系决裂。好歹在镇上买一套公寓，两三代人住在一起，他们也愿意。当然每个人都有每个人的想法，现在的女孩子出去嫁人的时候，特别看中房子。有的时候男性改变世界，女性改变男性的世界观，真的是没有办法。但是很多人不喜欢自己的故乡。如果连故乡都没有的话，心里并不是特别好的滋味，这样一个城市是没有腔调的。<br />
&nbsp; &nbsp; 上海是我的故乡，很多地方请我去，有的时候送我别墅。我说不是从上海出来的，这里是我的故乡，我希望我的故乡可以让每个来这里的年轻人有理想、有抱负，有一个自己容身的地方。<br />
&nbsp; &nbsp;…… 最后期望世博早日的开幕，如果是城市让生活更痛苦的话，我就希望它早日的闭幕。<br />
&nbsp; &nbsp; <br />
主持人：<br />
&nbsp; &nbsp; 我不知道大家经常去看韩寒的博客，偶尔我也看，韩寒的讲话还是像博客中那么直率。假设像韩寒这样的人，如果真的能够听到他说的话，总觉得我们的城市建得越来越好。<br />
&nbsp; &nbsp; 接下来为大家演讲的是陈丹青，就我们这个知识层面上，我最早能够看到谈论城市的公共知识分子就是陈丹青的文章。接下来把掌声给他。<br />
<br />
陈丹青：<br />
&nbsp; &nbsp; 大家好，我非常想知道几位领导对韩寒的评价，因为他的博客是没有声音的，我也想知道在座的年轻人是怎么想的。我很多人没有批评城市了，我在反省自己。韩寒刚才在咒骂城市，咒骂上海，我不知道怎么来形容今天的城市。但是他刚才把每一个神经都说到了，我非常想知道今天的城市建设者、城市决定者，也就是党的领导是怎么看待这个年轻人，这是很严肃的事。<br />
&nbsp; &nbsp; 关于今天这个论坛，一直到现在还不知道到说什么。我跟嘉定有缘分，1965年，我大概十一二岁的时候，我父亲带我到红桥区钓鱼，完全就是农村、稻田，我现在就住在那里，就是我过去钓鱼的时候。1968年我的老朋友郑学民（音），他带着我到南翔镇去画毛主席像，那里呆了将近1个礼拜。所以我跟嘉定是有缘分的。<br />
&nbsp; &nbsp; 我刚才来的时候注意到，外面的树很好看，他们说每一棵树2万块钱，都是移来的，但是我还是非常喜欢。我跟孙区长也有缘分，2002年左右他把我交到朱家角，关于保护江南水乡，保护一个旧的城区究竟应该怎么办，他要求我去。我当时刚回国，对国内的情况非常不了解，我主要就是咒骂，从古到今江南没有背景。<br />
&nbsp; &nbsp; 但是我现在发现，那个时候的说法太简单，不太有说服力。孙区长的决策跟我不一样，他要面对一个旧城保护、旧城建设，这是非常困难的。但很困扰的是，他们为什么叫我来，我为认为是所谓的艺术家，我觉得像我这样人来是没有用的。但是孙区长给我一个勇于，他希望我把话说出来，不管有没有用，但是对当时建筑家似乎有用。<br />
&nbsp; &nbsp; 今天嘉定区是一个崭新的城市，我没有东西可以咒骂。刚才进来的时候，很年轻一个建筑师，带我们看新城的模型。当我看的时候，和2002年之前面对所有新城建设时的心态不一样，因为我现在回国已经9年了，国家在教育我，我现在非常确认，而且非常佩服，这个国家，也就是党，做什么就一定能够做到，而且做的很好。<br />
&nbsp; &nbsp; 世博会场我还没有看过，这个主建筑太难看了，我不知道是谁设计的，如果这个设计者在这里，我想大声的说你怎么这么愚蠢，做出这件事情来，我知道这不是他的事，是领导的事。当时宣布举办世博会的领导人已经在监狱里了。<br />
&nbsp; &nbsp; 我不知道该说什么，我刚才看了这里的图非常好，什么都有了，幼儿园、小学、剧场，我能想到的都有了，我真的不知道说什么。我其实很想跟你们对话，你们的成就感是什么？你们的焦虑是什么？你们为什么要把我们叫来。<br />
&nbsp; &nbsp; 刘家琨我现在才明白，他是一个个体的设计家，好象不接政府的大工程，我最后明白，他会在这样的研讨会放出这个短片，很高兴她最后怀孕了。我<br />
&nbsp; &nbsp; 贾樟柯、韩寒，我，真的无助于将要建成的嘉定区新城，我想知道你们为什么叫我们来。我可以告诉大家一个真事，大概一两个月前发生的，南方周末举办一个很大的盛会，叫《中国梦大奖》，给中国各个领域有建树、有理想的人，最早的注意是从去年奥运会的开始的，因为张艺谋很成功的做成了开幕式，给全世界一个惊喜。南方周末想趁这个机会，给中国人一个梦。当时第一个考虑是颁给张艺谋，但是仗张艺谋不愿意出席，所以另外拟一个名单，最年轻的一个就是上海的韩寒。我知道韩寒根本不在乎这些事情，向这种的论坛都很少出现，如果真给你也未必会愿意去。<br />
&nbsp; &nbsp; 我被通知的时候，我是给韩寒颁奖，地点是上海，合作单位是上海文广集团。我发现接待我的这些南方周边主编、副主编，好象有话跟我说，已经晚上一两点钟了，还不离开我的房间。上海文广集团说韩寒不适合得这个奖，说他不适合。我觉得这是上海的羞耻，现在竟然容不下韩寒。我今天非常高兴能够请韩寒到这里来跟大家分享。<br />
&nbsp; &nbsp; 我想评一下孙区长，能不能对话一下，您期望我能说些什么？<br />
<br />
孙继伟：<br />
&nbsp; &nbsp; 我本来想作为区委领导坐在台下，我们请到5位嘉宾，想听听大家的真知灼见。刚才陈老师说，为什么要把他们请来，他们既不是建筑师，不是规划师，为什么要请来。我首先澄清一点，并不是想搞点热点，不是因为他们的名头比较大，吸引大家的目光，这不是我们的初衷。<br />
&nbsp; &nbsp; 我觉得从几点来考虑的。第一个是贾樟柯老实说的，在记忆城市的过去，还有一些人在思考城市的未来。作为城市建设者，作为城市建设的管理者也在思考城市的未来。我们的城市是以政府为主导的，快速的推动，我们的城市建设危险很大，纠正错误的时间也没有。我们非常希望有更多的人帮我们来看看未来的方向，我们非常担心大规模城市建设快速进行的时候，一不小心走错了，希望有更多的人帮助我们思考城市的未来。<br />
&nbsp; &nbsp; 今天在座各方面的精英，各方面精英思考问题可能更具有前瞻性，可以从城市不同的方面来思考。我一直在说，城市是在叫骂声中成长的，很多成功的城市都是在谩骂的过程中形成优美的城市。我们不要害怕谩骂，关键是要从谩骂当中汲取一些面向未来的东西。<br />
&nbsp; &nbsp; 刚才陈老师谈到韩寒，我也不知道韩寒，也没有看过他的书和博客，它们曾经提出这个名单的时候，我也感到很纳闷，他们说他在年轻人当中很有魅力。我们的城市是下一人建设的，可能刚才韩寒说了很多不正确的话，可能有我很不很赞同的话。至少有一点我认为是开放的，我的城市不仅仅是对我们这一代人，同时是为年轻人建设的，我们希望韩寒这样的人说说他们的想法、他们的希望，这样可以同样思考城市的未来。<br />
&nbsp; &nbsp; 我们的城市不仅仅是为某一中人设计的，我们希望有更多的人在这里生活，有更多吸引的人，或者有影响的人在城市里居住。他们希望是什么样的城市，所以就请来在座的各位来谈谈理想中的新城。<br />
<br />
陈丹青：<br />
&nbsp; &nbsp; 孙区长说话还是和5年前一样。<br />
&nbsp; &nbsp; 我试着比较体贴的来说这件事情，我发现像我们这类角色也很奇怪，比媒体弄成有名的人，似乎有资格被请到这种论坛。不管怎么样，是在变化的，我也要变化，我在想关于新起来的城市不打算再说什么，因为我有另外一种回忆。五六年代的回忆，当时上海的居住条件非常差，韩寒为今天城市种种荒谬做了一个景观式的描述。我也可以描述一个非常悲惨的上海弄堂里的生活。<br />
&nbsp; &nbsp; 不管中间发生了多少不公正，多少剥削和掠夺，不管怎么样，今天还是有很多人过上了在我们那个年代不可想象的生活，至少在住宿条件下已经好多了。我对已经造成的新房子不再批评，不再说一些情绪性的话。我要说的是，我相信你们在上海这个大区域里还是有焦虑，城市出来了，这么好的设施全都了，所谓有品质的生活，对未来能够说得出去的愿景，大约是什么？我想最后还是人的问题，我想知道，将来会有什么人会搬到这个房子来。过去说非常简单，一定有权优势的人才能搬到这个高档的房子里。假定一个是官僚阶层，一个是新富阶层，还有一个非常暧昧的阶层，又官又商的，境外人士住进来。我们所有官僚、新贵10年前、20年前都是穷人，都是一样的。共产党消灭了记忆，我不相信今天中国最有钱的人过一种生活我能起敬意，我看不起这种生活。<br />
&nbsp; &nbsp; 另外一个就是类似韩寒这样的人，比方说做广告的，做杂志的，IT行业的，能够买起楼。这个城市是为年轻仍造的，年轻人会带来活力。此外会想到，上海周边这些区跟上海是什么关系，上海在人文上已经堕落成一个地方城市，都是抬举它。它已经不是以前的上海，那个时候上海全国还会期待，看看上海今年有什么文化事件，拍出什么电影来，或者有什么新的有趣事情发生。现在没有了，全国对上海没有期待，甚至忘了上海。北京现在变成一个类似纽约的城市，我们现在不谈起上海。<br />
&nbsp; &nbsp; 最近贾樟柯受上海世博会委托，拍《上海传奇》，我也有幸参与，他是一个山西的孩子，从镇上出来。这些年他的电影拿到了很多声誉。这在50年前、70年前是不可思议的，那个时候上海是中国的摇篮，中国的文艺史、雕刻史差不多都是上海史。可是现在上海在文化上毫无发言权，无所期待的一个城市。<br />
&nbsp; &nbsp; 怎么办？我举一个例子，我最近刚刚有一个展览在维也纳，当时偷跑几天到布拉格去玩，有一个旧案要去考证，为什么当年莫扎特很多作用是在取得布拉格的确认，才到维也纳去。像他那样丰富的格局维也纳人还来得及消化。到布拉格首演的时候轰动全欧洲，回到维也纳才得到公认。布拉格还没有嘉定定，维也纳也没有上海大。为什么布拉格会那样？布拉格曾经是横跨东欧和西欧的中专占，从中世纪是欧洲最大的城市。不能想象莫扎特发迹在布拉格，他的首演剧场、首演的各种文物像当年那样保留着。<br />
&nbsp; &nbsp; 上海周边的郊区也有过骄傲，比如说最佩服的董其昌，董其昌是松江人，董其昌的伟大远远超出同时期的画家，可以当时有多少人知道他。我们翻那个时代，是整个中国元朝以来文人画出最密集的地方，江南一部分主的就是苏南，在17世纪董其昌成长的年代，应该想象这是文化极度富饶的区域。现在完全没落了，但是没关系，在未来30年、50年，我不再对上海抱希望，上海周边有没有希望，今天这个会议能够请到韩寒，也许还可以请到更有争议性的人。<br />
&nbsp; &nbsp; 现在真正有正气的人，真正对文化造成影响的人其实已经很少了，可是可能性又空前的大，这么多硬件在这里。比方说愿不愿意请摇滚乐团进来。我可以再说一件事情，文化部今年春夏之交让我去，只有不一个人是个体画家，通过艺术文化管理条例，从出售到到出入境都有详细的条款。我很高兴，文艺家今天终于不是任人宰割，能够很大胆的说出自己的创意。<br />
&nbsp; &nbsp; 我在美国呆了这么久，所有的文艺都许多管理条例，最重要的是色情业。文化部官员连色情文化管理条例都没有出现，就出台文化管理条例。我建议先出台色情文化管理条例，好好管理，不要出现事件。我们可以说这是国情，那么多官员出事都出在色情上，谁没有情人。一个有人性的政府，一个有人性的党，一定是有色情的文化。所以不要怕这个事情。可以认真考虑这个问题。青年人就是，音乐、展览、色情业，餐饮业。<br />
&nbsp; &nbsp; 我其实并不反对建迪斯尼，迪斯尼并不仅仅是游乐场，带进来的是整套文化，香港也是经过多年的争议，也落地了。我觉得比迪斯尼乐园糟糕一百倍的事情天天在发生。我觉得把未来开放给各种各样的可能性。唯一一条就是不能有禁区。<br />
&nbsp; &nbsp; 我拼命的想，只说这些，大约就说到这里。<br />
<br />
主持人：<br />
&nbsp; &nbsp; 本来有一个茶歇的时间，现在时间比较晚。<br />
&nbsp; &nbsp; 为了广泛传播世博理念，激发公众对城市生活品质的自觉意识，11月3日，《解放日报》、《文汇报》和&quot;上海嘉定&quot;政府门户网站，同时刊载&quot;相约嘉定新城，关注城市生活&quot;有奖问答，征询公众对于此次论坛的意愿。此次论坛组织方从众多应答者中，选出优秀答题者10名，给予奖励。奖品为世博会门票和马陆葡萄主题公园2010年度会员卡。该会员卡可以在公园领养一棵葡萄，随时预约，带着孩子免费进园观察葡萄发芽、生长、施肥、开花、结果的全过程，并收获这棵葡萄上全部的果实。如果遇到自然灾害或其他灾害，可以获得10公斤葡萄补偿。非常具有诗意和情趣的奖品。<br />
下面，我受论坛组织方委托，宣布10位获奖者名单，他们是：<br />
柯贤鹏、栗爽、顾昊、江建军、赵树利、骆军、李学峰、邵文斌、杨帆、丁浩。<br />
论坛组织方从10位获奖者中抽选出3位（柯贤鹏、栗爽、顾昊），来到今天的现场。他们除了获得奖品，还可以享有向台上的演讲嘉宾提问权。<br />
在公开征集的20道问答题中，有一部分是意愿选择题。我们在这里把这些公众关心的话题，征求几位嘉宾的答案。<br />
<br />
栗爽：<br />
&nbsp; &nbsp; 使用和美观二元系统中，很强调使用的系统，使用的东西，比如说您的观赏的桃子和吃的桃子，使用的东西会不会带来管理的变动？<br />
&nbsp; &nbsp; 第二个，很多人未必喜欢这样的东西，我们觉得金鱼是好的，鲫鱼是不好看的。我想在推行各种建筑方案的时候，可不可以以大多数人的观点来作为标准？比如说农村，农民觉得这样是好的，即使我们觉得不好，但是也按照他们的想法来。<br />
<br />
俞孔坚：<br />
&nbsp; &nbsp; 很多美都是设计师给他们的，老百姓被集中安置在住宅区，让他们种花，结果还要种菜，他们让农民把菜拔掉。我一直在论证这个问题，开始建世博园的时候，要用农作物当地的植物，做好真的很美。使用的东西，生产性的东西，比如说稻田往往觉得是农村的，把稻田拆掉，种上草坪。稻田的管理比草坪管理还要简单，连小孩子都知道怎么样种稻田，但是你必须是园艺专业毕业的学生才会知道怎么样管理草坪。这也是价值观问题，也是我了解很多的情况，不知道在座的各位是不是有同感。<br />
<br />
顾昊：<br />
&nbsp; &nbsp; 我今天想问的是，新城规划理念很先进，前景很漂亮，但是现在注重的是动脉产业，怎么样生产，我们是不是关注下一个动脉产业，每时每刻都会产生垃圾，新城是不是有这样方面的做法，把垃圾处理的更好？<br />
&nbsp; &nbsp; 我还要说一点，我很欣赏韩寒的话，感谢他替年轻人说话，房子太贵了。<br />
<br />
俞孔坚：<br />
&nbsp; &nbsp; 首先设下水道不是明智的做法，我们在天津就取消了下水道，因为水的功能有多种多样，可以用来滋润土地，可以用来用鱼，而且很漂亮，为什么要用下水道里。取消下水道是国际先进的做法，第一可以节省投资，第二可以发挥水的作用。土地和城市如何作为整体来设计，下面的雨水进入土壤，进入湿地系统，而不是下水道。<br />
&nbsp; &nbsp; 简单的讲，现在城市里面设计的马路压都是错的，为什么？因为所有马路牙都是把水挡在地下，而不是排在地里面，而且抽地下水灌溉草坪，这也是误区。可以实现你刚才讲的，一个生态城市一定是科学理解这些。<br />
&nbsp; &nbsp; 城市还是朝着汽车的城市，为什么一开始讲我本人是反对汽车，我现在上班都走路，远一点汽车。为什么？第一汽车不健康，第二给环境带来压力，第三把城市变得很不人性。现在城市里的用车量没有达到饱和，再过几年，这个城市就走不动了。<br />
&nbsp; &nbsp; 如何来解决这个问题，城市就应该设计成绿色的城市？嘉定和上海是卫星城的关系，嘉定完全可以通过步行、自行车来解决问题。城市布局一定要考虑综合的，土地的使用要混合使用，而不是拆卸成一个机器，那是100年前的旧概念。现在搭建的城市真正是一个整体。<br />
&nbsp; &nbsp; <br />
主持人：<br />
&nbsp; &nbsp; 现场的听众有什么问题？<br />
<br />
提问：<br />
&nbsp; &nbsp; 我想问一下在座的嘉宾，你们认为作为一个新城怎么样才是可持续发展的？什么样的郊区生活才是我们理想钟的生活？<br />
<br />
陈丹青：<br />
&nbsp; &nbsp; 我相信这是一个没有的问题，但是它又很真实，全中国这些年来的城市运动，都是用一个词就是打造，由谁来打造？就是政府和开发商。这种是很少见的现象，历史形态的乡村和城市是慢慢成长起来的。现在什么都弄好，你买来生活。这是一个不成立的问题，同时又很真实。所以做事情的人很难，弄了这么多房子给你们，你们还不满意。<br />
<br />
俞孔坚：<br />
&nbsp; &nbsp; 今天这几位嘉宾都回答了这个问题，比如说贾导讲了这个故事，对城市要有记忆，人不是动物，人是需要有历史的，需要有记忆，需要有认同感和归属感，人是有感情的动物。这是一个方面。<br />
&nbsp; &nbsp; 第二个方面，人又是自然的人，要呼吸新鲜的空气，健康的水，干净的食物。城市是不是满足这个条件，是不是觉得这个马路太危险了，如果水是观赏用的连碰都不敢碰，这个城市不是好的城市。如果城市里面连鸟都看不到，这个城市是不好的城市。如果走在街上，看到的东西都不是真实的东西，那跟这个土地没有任何的关系。<br />
&nbsp; &nbsp; 为什么刚才讲造了很大漂亮的房子，不光是设计好，我们要设计一个家，这个家是在这个土地上的住所，在这个土地上可以找到自己的位置。刚才刘家琨讲了，连你的床头照片都不是你的，都是虚假的，城市都在造假。河都是假的，水是假的是喷泉，房子是假的，有可能是美国、加拿大来的，连我们看到的植物都是假，看不到身边的芦苇、看不到身边吃的粮食，看的都是园艺的杂交品种。所以你就失去了自然的文化，这个城市是假的。<br />
&nbsp; &nbsp;理想的城市，最根本的一点是真实的生活在这个土地上，而不是虚假的，我们现在追求的都是虚假的，虚假的街道，没有陆地。<br />
<br />
刘家琨：<br />
&nbsp; &nbsp; 这是多少代博士生都在做的论文，新城是容易做到的，但是理想每个人都不一样，我觉得一个好的城市应该是维护大多数人的利益，但是容纳所有的选择。谢谢！<br />
<br />
韩寒：<br />
&nbsp; &nbsp; 这个问题实在太大了，我觉得我也回答不过来，刚才我也说了一些，在这里就不多说了。新城能够让你负担比较小，能够在这里生活，这就是现阶段的理想。<br />
<br />
提问2：<br />
&nbsp; &nbsp; 各位嘉宾好。我前两天刚刚看一本书，刚一个社会学家写的，他写是整个香港转型过程中市民的生活状态，当时他提出一个香港精神。我今天好好奇想问一下，如果你们认为上海有精神的话，未来的上海精神应该是什么样的？<br />
<br />
贾樟柯：<br />
&nbsp; &nbsp; 毕竟这一年历跟将近100个上海人聊天，聊他们的过去，他们的生活。对我这个北方人来说，我们对上海人的误解非常多。作为城市里面的生命个体，普遍呈现出来什么样的精神，过去上海那种包容性、开放性，这是给我留下很深的印象。现在各种各样的讲话、套话太多了，根本不过脑筋，只要是上海领导讲话都会说上海海纳百川，如果追溯历史，真的感受海纳百川，过去上海对所有东西的包容性是让人非常震撼的。从电影来说，最早的电影放映在上海，以前好莱坞亚洲的首映都放在上海，能想象到那个时候上海的青春、活力。如果是我能感受到精神的话，过去的活力也来自于，上海是一个移动城市，最早广东人来做木材生意，后来做茶叶，后来洋人来了，全中国人涌入了，因为人口移动带来的活力，是这个城市给我特别深的印象。<br />
<br />
刘家琨：<br />
&nbsp; &nbsp; 我从四川到这里工作，一个感受是上海还是比较包容的，有自信的，我希望这种自信心能够继续发扬。上海跟四川人不一样，事先会把事情说好，比较麻烦，但是事后就比较顺。另外一个状态，我觉得江南是一个文化城市，做事比较讲究。从我自己的感受来说，我觉得比较包容、比较自信，发扬做法精致的精神。<br />
<br />
韩寒：<br />
&nbsp; &nbsp; 这个问题不是在座人可以谈的，是市委书记拍板的，说一个上海精神，第二天在报纸上出现的就是上海精神个。<br />
<br />
主持人：<br />
&nbsp; &nbsp; 我代表主办方感谢今天到来的5位嘉宾，总体感觉是5位嘉宾都非常真诚，贾樟柯导钱包都放在桌上了。说的非常好，用情都很真，无论是专业知识的到位，还是其中的冷嘲热讽，但心中都有这个理想。我们今天站在一个非常好的词下面，那就是理想。今天背景的河里面有一朵美丽的莲花，不管这是上海的那条河，我们都希望在上海每一条河里都能看到莲花在开放。<br />
&nbsp; &nbsp; 今天的论坛到此结束，谢谢大家！<br />
<br />
（结束）&nbsp;&nbsp;<br />
