<br />
==================<br />
<br />
……新疆……写下这两个字让我颇费踌躇，它是中国现实领土六分之一面积的称号，生活在那片土地上的两千多万人民时刻挂在嘴上的名称。但是当我在头脑里面对这本书的可能读者时，会浮现我在波士顿经历的场面。那是一个关于「族群」问题的研讨会，到会的有藏人、蒙古人、台湾人，还有大陆汉人。大家都知道，如果没有维吾尔人代表，该到场的肯定不能算完整。当会议已经开始，才有一位维吾尔人从德国姗姗来迟。他的第一句话是向与会者宣布，如果有人使用「新疆」二字，他便拒绝参加会议。<br />
新疆……一旦进入某种场合，就从一个地名变成包含很多难题和对抗的历史。什么是「新疆」？――最直接的解释是「新的疆土」。但是对维吾尔人，那片土地怎么会是他们「新的疆土」，明明是他们的家园，是祖先世世代代生活的土地呀！只有对占领者才是「新的疆土」。维吾尔人不愿意听到这个地名，那是帝国扩张的宣示，是殖民者的炫耀，同时是当地民族屈辱与不幸的见证。<br />
新疆――即使对中国也是个尴尬地名。既然各种场合都宣称那里自古属于中国，为什么又会叫做「新的疆土」？御用学者绞尽脑汁，把「新疆」解释成左宗棠所说「故土新归」，却实在牵强，那明明应该叫「故疆」才对，怎么可能叫「新疆」呢？何况早在左宗棠前一百年，那片土地就已经被清王朝叫做「新疆」了。<br />
不过，只要谈那片广阔土地上的事，总得用一个名称。最终我还是用了「新疆」，除了是一种现实的不得已（即使是东土人士谈具体话题也难避免用「新疆」），其实也能让双方都从中各取所需――维吾尔人能以此证明他们的土地是被中国所占，中国也能以此宣示疆土的归属。<br />
用这么多篇幅，我的目的不是仅为说明选择地名的困难，而是想说明新疆问题的复杂。仅地名就已存在如此纠葛与对立，揭示新疆问题全貌的困难可想而知。<br />
这本书写作的起点，应该是一九九九年。那时我刚出版《天葬――西藏的命运》。再写一本新疆问题的《天葬》是我最初的想法。如果没有在新疆入狱，那写作应该会按部就班地进行，书也会在几年前就已问世。不过那样写出的书一定和这本不一样。它会像《天葬》有个面面俱到的框架，居高临下地概述，力图包容新疆问题的全貌。但是当我被关进新疆的监狱，被勒令从此不可再触碰任何官方资料，使我不得不放弃框架式的写作。新疆问题的真实信息几乎都被封闭在<br />
- 9 -<br />
官方数据内。没有官方资料，框架是建不起来的。<br />
不过这却可以算作一种成全。入狱使我更深地进入了新疆的情景。当我准备继续写这本书时，已经变得踌躇渐多，不再觉得有资格搭建框架和居高临下地概述，更不敢轻易给出结论。入狱是这变化的转折点。当监狱之门在我身后锒铛上锁，进入新疆的另一道门却悄然打开。那道门内的新疆不再是文件、书本和信息中的符号，而是真实的血肉、情感乃至体温。我与新疆的土地和在那片土地上生活的人们从此有了脉络相通、呼吸与共的感觉。<br />
于是，我不再为缺少官方信息而遗憾，也不再认为那是缺陷。信息不是真理，甚至不一定是真相。没人能比统治者得到更多信息，却不能说统治者了解了事物真相。历史让我们看到，即使是在殖民地过了一辈子的殖民者，又何尝懂得那里的人民？我写新疆，重要的不在罗列信息。哪怕是掌握最核心的官方秘密，价值也不如去展现这块土地上的人民，去了解他们的生活、情感和愿望。<br />
这无疑非常困难。不错，在新疆境内，每天都可以见到维吾尔人、哈萨克人、乌孜别克人……作为一个汉人，你可以跟他们打交道、做买卖、讨价还价，也许还可以开个玩笑。但所有这些都不意味你能进入他们内心。在汉人面前，他们把内心严密地包藏起来。从一九八○年，我前后九次到新疆，走遍了新疆每条主要公路，到过所有地区，五次翻阿尔金山， 三次穿塔克拉玛干沙漠。那虽然花费很多时间，耗费不少资财，但却比看见一个维吾尔人的内心要容易。可以说，直到我入狱前，走遍了新疆的我，没有一个维吾尔朋友。即使在维吾尔人最集中的地方，我也只能出入汉人圈子。不是我没有接触他们的愿望，是他们不接纳。每天在眼前掠过的维吾尔人，仅仅是街道或巴扎（维吾尔语：集市）上的影像。<br />
至今，我未见任何汉人研究者真实展现过维吾尔人的内心。中国官方近年对新疆研究投入很大。众多官方研究者有权看文件，了解机密，见的人广，到的地方多，却唯一做不到打开维吾尔人的心扉。对此，海外维吾尔人的发言并非可以全部弥补。他们可以讲新疆境内没人敢讲的话，但是并不完整。角色的对立使他们的话语与中国官方泾渭分明、黑白相反，展现的往往是政治姿态和组织立场。而我们更需要知道的，是生活在新疆境内的维吾尔人内心想什么。在我看来，能听到一个维吾尔人的心里话，绝对胜过读一百本外人写新疆的书。<br />
如果没有新疆入狱，我永远不会有这样的机会。穆合塔尔是我的同牢狱友。在今日中国，能让维吾尔人接纳汉人的地方，大概只有关押政治犯的监狱。那次入狱给我的最大收获就是结识了穆合塔尔。这本书正是因为有了他，才有了现在的角度――不再居高临下，而是置身其中；不再用外人眼光，而是站到了维吾尔人中间。<br />
这本书的内容是在不同时间所写，但都和穆合塔尔有关。第一部分是我离开监狱后的追忆，记录了我被捕经历，包括与穆合塔尔的相识。<br />
- 10 -<br />
第二部分是我出狱后四次重返新疆的经历，是根据当时的旅行日记编写。四次我都和穆合塔尔见了面。新疆对我的吸引，穆合塔尔已经是主要因素。那四次游历几乎覆盖了整个新疆（只有北疆一角未到）。没有机会自己游历新疆的读者，不妨利用我的眼睛，尽管走马观花，却至少是了解新疆的基础。<br />
第三部分是本书的重点――我对穆合塔尔的访谈。那是按现场录音整理出来的，除了理顺口语，基本保持原貌。你会如同坐在我的位置，倾听一位维吾尔人敞开心扉。那席话将会带你直接进入新疆问题的核心。<br />
第四部分是我对新疆问题的思考。写在我给穆合塔尔的信中。虽然被放在书的最后，却不是结论。本来计划等待穆合塔尔回应，和我的信放在一起再出书。但是关系到维吾尔民族命运的话题，光靠写几封信是不够的，需要由穆合塔尔写出自己的书。<br />
我为此书致谢的人可以开出长长名单，然而还是像以往在中国境外出书一样――出于安全考虑无法公开。我只能心怀感激，默念名单中的所有名字。排在最前面的当然是穆合塔尔。原本我用××××代替他，但是显而易见，那不能让需要防范的人不知道他是谁，只能让对他无害的读者不知道他是谁。从这个角度，公开他的名字不会更有害，也许还能对他多一点保护。<br />
不过我仍然心存忐忑，祈求这样做不会是一个错误。我在写这本书的时候曾梦见，我和穆合塔尔又坐在同一间牢房。不过我们已经没有恐惧，没有忧伤，好像那就是该有的命运，只是安静相对，等待把牢底坐穿的一刻。<br />
二○○七年一月十八日 北京</div>

							
							
							
							
