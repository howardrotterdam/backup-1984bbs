													<h2>《奇迹的黄昏》袁剑著</h2>
						
						
																											<div id="postmessage_451695" class="t_msgfont">正在看《奇迹的黄昏》，极为震撼，可能很多人已经看过了，没看过的推荐大家看一下。<br />
<br />
序:绝望的思想<br />
这又是一本不太可能出版的书，之于我，这种经历已经是第二次了。奇怪的是，我似乎并没有特别的不快。一是因为我可能早已经习惯了中国对思想著作特别苛刻的出版环境。更为重要的是，我对这本书本身的价值发生了深刻的怀疑。如果有可能重写此书的话，我肯定会做出重大的修改甚至推倒重来。但我也知道，即便如此，在最后一刻等待我的，仍然将是彻底的绝望。当然，这不是对自己的绝望，而是对人类思想及人类认识能力的一种终极的绝望。如果喜新厌旧是人类天性的话，今是昨非就是所有思想的本性。<br />
《奇迹的黄昏》的绝大部分写就于2004 年。就其主题而言，是我前一本同样未能出版的《中国的变局》修订本。《中国的变局》完成于1995年，那时我还是一个三十岁的年轻人，中国改革本身的面貌和结局也远远没有清晰的呈现出来。十年之后，当我重新落笔的时候，中国的情况几乎天翻地覆，而我自己也已经进入越来越迷惑的不惑之年。所以，说是对《中国的变局》的修订，但实际上却几乎是完全的改写。<br />
多年以来，中国问题一直是我的思想职志。中国问题的巨大复杂性一方面给我带来了空前的精神折磨，另一方面却使它具有无与伦比的知识吸引力，成为一个思想黑洞，让我们不断地飞蛾扑火。对我个人的精神生活而言，这本书的大部分意义可能就在于：在不断的折磨与扑火之间所获得的那种生命的张力。当然，对中国人在中国改革中的命运所寄予的深切关注和同情，可能是本书的另外一个精神源流。中国改革是一场滔天的洪流，所有中国人的命运都为之巨变。无论是幸运儿还是失意者，也不管是登峰造极的英雄还是命运凄惨的弃儿，都是这场巨大历史活剧的一部分。他们的命运值得我们去关注、去同情、去感同身受。在这个意义上，这本书是献给他们的。<br />
作为一个理想主义者，我无法站在那些现实主义者的立场上来评价这场改革。所以其中的批判性在所难免。在群星闪烁的夜晚，我时常仰望星空无限感慨：自诩为无所不能的人类为什么就发明不出一种人人都幸福、人人都快乐的文明？这种肯定会被智者讥讽为幼稚的慨叹，在我这里却始终挥之不去，如影随形。或许，这个问题只有上帝才能回答。但这同时透露出一个相当残酷的事实：人类离真正的智慧还有何等遥远的距离。或者，这根本就是一段不可逾越的距离。如果读者有机会读到这本书，我特别希望他们不要忽略了本书的这样一种精神气质。就在我写作这篇序言的时候，几十年如一日笔耕不辍坚决捍卫自由市场的林行止先生宣布改宗：从一个自由主义者变为一个社会主义者。我相信，中国绝大多数弱智的自由主义者都会为此惊奇得张大嘴巴、跌碎眼镜。作为一个早期的自由主义者，我非常能够理解林先生心中的那份煎熬和彷徨。只不过，我不会像它那样随便抓住一根稻草，就开始自我的精神救赎。<br />
最近几年，在朋友闲聊的场合，我反复唠叨的一句话是：世界是上帝创造的，知识和逻辑是人类发明的，其中永远隔有一堵神秘的无知之幕。换句话说，我们根本无法达成对这个世界的完全理解。而这种不完全理解，实际上将我们置于了一种完全无知的状态。就像人类所面临的唯一确定性是死亡一样，思想面临的唯<br />
一确定性则是无知。对于一个以思想者自我期许的人来说，还有什么比洞悉了这一结局更加不堪的呢？对于我来说，这几年几乎已经陷入不能写，不能说的境地，因为我知道一写就错，一说就错。人类因思想而生，也因思想而死。所以，我特别羡慕那些纯粹的某某主义者，无论他们是自由主义者，还是社会主义者，也无论他们是年轻的追随者，还是年迈或故去的祖师爷们。不是因为他们“知”着，而是因为他们“生”着。虽然他们之中绝少有人意识到，他们的“知”不过是一种信仰，但是也正因为这样，他们的生命却变得盎然。他们因思想而生，因思想无限展开的可能性而生。而在我自己这边，却是另外一幅黄昏景象。思想之门因我对思想的绝望而逐渐闭合，生，也变得如此沉默和萎缩。如果可能，我愿意匍匐在主的脚下，任其主宰和驱策，成为它的一部分，并最终达到对生命和世界的全部理解。但是我知道，主的门不会向我打开。因为我们是异教徒，理性主义早已经将我们放逐到了不能返回的地方。事实上，自启蒙时代以来，我们就被所有的确定性放逐了，它任我们在无边无际的可能性中游荡、飘浮，像一群孤魂野鬼。然而我了解，这就是我们的命运。<br />
<br />
袁剑<br />
2008年仲夏于南京</div>

							
							
							
							
