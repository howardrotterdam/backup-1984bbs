　城市在燃烧。乡村在燃烧。举着太阳旗的日本兵。逃难灾民。被侮辱的妇女。半死不活的孩童。倒在血泊中的抗日将士。一个又一个幸存者的血泪述说。那是中国人昨天的哀伤记忆…… <br />
　　 <br />
“台视”播放系列片《一寸河山一寸血》，创造了可观的收视率。台湾卓越文化传播公司制作这部长达42集的电视节目，前后长达两年时间，边播放还在边修改。这在快节奏的台湾社会实在不多见。 <br />
　　 <br />
为什么要开创台湾电视纪录片的这个“第一”？这位才华横溢的著名电视编导陈君天沉吟片刻，缓缓地说：“我们中国人近百年来没有尊严，死了那么多的人，非常冤枉。一直到今天为止，还有人认为日军侵华不是真的。不少人已经把这个事情慢慢地淡忘了。 <br />
　　 <br />
“抗日战争里，我们中国人死伤了有3 0 0 0 万！对于那么一个庞大的、惊心动魄的战争，应有一个非常持平、非常公正、非常真实的记录。事实上，我身为一个中国人，在整个制作过程当中有许多痛心疾首的地方，我力图把这些感觉留给后人……” <br />
　　 <br />
“卓越”追求的是卓越。他们曾不辞劳苦，前往日本、美国、俄罗斯和祖国大陆收集胶片、图片和文字资料。在莫斯科的档案馆找到侵华日军的一些纪录片，一分钟胶片就要花6 0 0 美元，咬着牙买下来。 <br />
　　 <br />
摄制组访问了近8 0 0 名见证人，采访对象分布在世界各地，包括在台湾，在祖国大陆，还有在美国、在日本的。他们到祖国大陆云南腾冲、滇缅公路访问过，还去拍过东北“7 3 1 ”部队旧址、南京<br />
大屠杀纪念馆、上海淞沪战役的故地。被访问的很多人年龄都在7 8 岁以上，从7 8 岁一直访问到1 0 3 岁的老人。问他们早上吃什么可能全忘了，唯独战场上的事他们忘不掉，记忆非常深刻。 <br />
　　 <br />
对应被采访者的话，一点点地找画面编太难了。有的是现拍的，有的是照片，有的是老影片。有时他们找到的一部影片资料太差了，得从别的影片里选一些，拼图一样组合着拼出来。有时已经播出了，后来又发现新的资料，只要发现这个资料更好，他们马上重新整理，用最好的画面来填补一个以前的空白。 <br />
　　 <br />
片子播出之后，“卓越”办公室的电话响个不停。陈君天当初想，这个片子，恐怕主要收视群是年纪大的观众，因为他们会有自己的感情在里面，但他很惊讶，播出以后他们收到最多的电话和来信，是台湾土生土长的年轻人打来或写来的。年轻人说，看了片子感动极了，过去不知道啊！ <br />
　　 <br />
在陈君天的名片上，印着他的座右铭：把历史还给历史，让真相长白千秋。这些给我们一个很大的鼓舞。我们的目的就是让下一代知道，抗日战争这8 年之间所发生的事情。民族的苦难和尊严，所有中国人都不该忘记。 <br />
