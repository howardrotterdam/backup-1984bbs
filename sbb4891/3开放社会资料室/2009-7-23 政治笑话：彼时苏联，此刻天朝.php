一个外国人在拥挤的公共汽车上问：在车上和我们在一起的都是什么人？<br />
答：他们是国家的主人。<br />
“而这是什么人开到前面去了？”他指着超过他们的”伏尔加”问道。<br />
答：这是人民的仆人。 <br />
<br />
102.<br />
问：数学和科学共产主义有什么区别？<br />
答：在数学上，如果给出什么东西，都需要证明，而科学共产主义什么都能证明，就是什么也不能提供。 <br />
<br />
103.<br />
问：从马克思主义的观点来看，什么是短缺？<br />
答：就是不被我们所感觉的客观现实。 <br />
<br />
104.<br />
问：什么是全国的电气化？<br />
答：这就是共产主义减去苏维埃政权。 <br />
<br />
105.<br />
问：赫鲁哓夫是怎样改变了列宁的共产主义模式的？<br />
达：共产主义，这就是苏维埃政权加上全国的玉米化。 <br />
<br />
106.<br />
问：听说苏联进军捷克斯洛伐克后，就会实现共产主义？是真的吗？<br />
答：是的，电气化在那里已经实现很多年了。 <br />
<br />
107.<br />
马克思想在苏联发表广播演说。勃列日涅夫对他说：虽然您是人道主义的奠基人，但是我一个人不敢决定那么重大的问题，们是集体领导。马克思说：我只想说一句话！<br />
最后，勃列日涅夫只允许说一句话，并且要他自己负责。马克思凑近话筒，大声说道：全世界的无产者，请原谅我！ <br />
<br />
108.<br />
列宁告诉我们怎样进行统治，斯大林告诉我们不可以怎样统治，赫鲁哓夫告诉我们所有傻子都可以统治，勃列日涅夫告诉我们不是所有傻子都能统治。 <br />
<br />
109.<br />
问：为什么列宁穿皮鞋，斯大林穿靴子？<br />
答：列宁时代俄罗斯只脏到脚脖子。 <br />
<br />
110.<br />
人们在地狱里问导游：为什么希特勒站在大便里，只是没到脖子，而斯大林只是没到腰上？<br />
导游：因为斯大林站到了列宁的肩上。 <br />
<br />
111.<br />
列宁时代像是在隧道里，周围一片黑暗，只是在前面有光；在斯大林时代，像是在汽车里，一人领路，一半人在上坐着，另一半人在上颠簸；在赫鲁哓夫时代，就像在参观团里一样，一个人表演，其他人在笑；在勃列日涅夫时代，像看电影一样，大家都等着散场。 <br />
<br />
112.<br />
在斯大林格勒更名为伏尔加格勒后，有另一个世界来了一封电报：”同意。约瑟夫&#8226;伏尔加”。 <br />
<br />
113.<br />
问：为什么安德罗波夫，而后是契尔年柯在共产党上被一致推选？<br />
答：这是因为安德罗波夫的肾检验结果最差，契尔年柯的心电图最差。 <br />
<br />
114.<br />
一个公民来参加契尔年柯的葬礼。有人向他要票，他答道：到这里来我用的是联票。 <br />
<br />
115.<br />
问：什么事赫鲁哓夫打算做而来不及做？<br />
答：嘉奖尼古拉二世，是他在俄罗斯创造了引发革命的条件。 <br />
<br />
116.<br />
问：大家都知道，在赫鲁哓夫下台后的头一个十月革命节，人们把阿弗洛尔巡洋舰沿运河运到莫斯科，这是为了什么？<br />
答：向临时政府射击。 <br />
<br />
117.<br />
在列宁格勒正在拍摄关于十月暴动的电影。布尔什维克正在进攻，军人在射击。看热闹的人群中有人喊道：亲爱的，坚持到最后！ <br />
<br />
118.<br />
一个年老的女演员想早一点获得国家演员的称号，人们就建议她入党。在入党仪式上人们问她怎么看共产主义。女演员说：所有东西都是充足的，不论是食品还是商品，还允许出国，总之，像在沙皇时代一样。 <br />
<br />
119.<br />
美国外交代表团到苏联访问，苏修接待官员陪他们参观建设的伟大成就，并且得意的说：到了下一个五年计划，每个苏联家庭都可以拥有一架私人飞机！<br />
美国人惊讶的问：他们要飞机干什么呢？<br />
苏修官员说：当然有用啊……譬如你在莫斯科听说列宁格勒开始供应面包了，你可以马上开飞机赶去排队啊。 <br />
<br />
120.<br />
在二战末的一次国际会议上，斯大林嘲笑已经在国内竞选中失败的丘吉尔：丘吉尔先生，你打赢了战争，人民却罢免了你；看看我，谁敢罢免我！<br />
丘吉尔平静的回答说：我打仗就是为了保卫人民拥有罢免我的权利。<br />
<br />
121.<br />
 勃列日涅夫，杜布切克(捷克总书记)，英国首相和美国总统，4人乘坐气球。<br />
气球漏气了，抬不动4人，开始下坠，万分危机，必须有人牺牲自己跳出。<br />
先是美国总统喊了声“为了自由世界!”，然后跳了出去。<br />
气球下坠暂缓，但过一会儿漏气更多，下坠又加快，必须再跳出1人。<br />
于是英国首相喊：“为了女王陛下!”，跳了出去。<br />
暂缓一会儿又不行了，于是勃列日涅夫喊道：“为了社会主义大家庭!”说着就把杜布切克扔出去了。<br />
<br />
122.<br />
列宁的夫人曾经当面指出斯大林的专制独裁统治，令后者气急败坏，斯大林说：你再说，我就宣布你不是列宁的妻子！ <br />
<br />
123.<br />
一个莫斯科市民的鹦鹉丢了。这是只会骂人的鹦鹉，要是落到克格勃的手里可糟了。这人便在报纸上发表了一篇声明：本人遗失鹦鹉一只，另外，本人不同意它的政治观点。 <br />
<br />
124.<br />
赫鲁晓夫和肯尼迪交谈，各自吹嘘，肯尼迪说：美国医学发达，有种药片死人吃了可以复活。赫鲁晓夫说：苏联体育发达，有人十分钟可以从莫斯科跑到美国。<br />
肯尼迪要求兑现，赫鲁晓夫慌了手脚，召集文武大臣商量对策，有人出了个好计策说：这很好办，你先让肯尼迪把药片拿来，让斯大林吃了，那斯大林一定复活，那么你用不了五分钟就可以从苏联跑到美国。<br />
<br />
125.<br />
地狱有个规矩，谁在人间害了人，被害人的血将淹此人。一次上帝去地狱视察，发现血只浸到贝利亚的腿。上帝就很奇怪，问到：你杀了这么多人，怎么血只及腿呢？贝利亚答道：因为我站在伟大领袖斯大林的肩上！<br />
<br />
126.<br />
越战时期，河内物资奇缺，向苏联求救。<br />
苏联电：勒紧腰带。<br />
河内回电：请给腰带 。 <br />
<br />
127.<br />
一位美国历史学家和一位俄罗斯历史学家，讨论谁领导廿世纪的前半世纪。<br />
“我投票赞成胡佛先生，”美国人说，“他尝试着教导我们美国人停止酗酒！”<br />
“那个没什么了不起！”俄国人接着说，“我选择斯大林，他尝试教我们俄国人不要吃饭。” <br />
<br />
128.<br />
斯大林在大会上引经据典地说：”马克思和列宁说1+1=2，而托洛茨基和布哈林说1+1不等于3。是托洛茨基和布哈林说的对呢？还是马克思和列宁说得对呢？”下面听众一脸疑惑。<br />
“毫无疑问，是马克思和列宁说的对！”底下热烈鼓掌，“托洛茨基和布哈林是帝国主义派来的间谍，说1+1不等于 3的人罪不容赦……” <br />
<br />
129.<br />
当年的捷克斯洛伐克政府中，设立了一个海军部，结果，苏联老大哥就对捷克人说：你们是内陆国家，设什么海军部？<br />
捷克人回答说：那你们不是也设了文化部吗？ <br />
<br />
130.<br />
勃列日涅夫：同志们，美国人登上了月球，我们不能再等了，党决定让你们上太阳。<br />
宇航员：总书记同志，我们会被烧死的。<br />
勃列日涅夫：没关系，同志们，党都替你们想好了，你们晚上去。 <br />
<br />
131.<br />
二次大战时，罗斯福，丘吉尔，及斯大林三位巨头在雅尔塔开会。罗斯福，丘吉尔处处被动，因此两人合计着想算计斯大林一把。<br />
丘吉尔说：“昨天我作了一个梦，梦见自己成了地球的主宰！”<br />
罗斯福也接着说：“昨天我也作了一个梦，梦见自己成了宇宙的主宰！”<br />
两人说完后，得意地瞧着斯大林。斯大林微微一笑，说道：“昨天我也作了 一个梦，梦见自己既没有批准对丘吉尔先生的任命，也没有批准对罗斯福先生的任命。” <br />
<br />
132.<br />
两男一女在沉船后沦落荒岛。<br />
如果两个男的是西班牙人，他们会用决斗来决定谁拥有那个女人。<br />
如果他们是法国人，一个是女人的丈夫，另一个是她的情人。 　<br />
如果他们是苏联人，则会把报告书塞入瓶子，让水流把它漂回祖国，请上级指示他们谁可以娶她为妻。 <br />
<br />
133.<br />
一个美国人刚从莫斯科机场出来，他想打听一下时间。这时从身旁走过一位莫斯科人，手里拎着个沉重的箱子，很吃力的样子。美国人拦住了他，问他当地时间。莫斯科人放下箱子，抬手看了看表，一口气报出来现在的时间然后还有气压温度湿度等信息。美国人很吃惊，问道：这块表的功能可真多，是苏联的技术生产的吗？<br />
莫斯科人自豪地回答：是的，是我们苏联技术制造的。<br />
然后又吃力地重新拎起那个箱子接着说道：就是电池重了点。 <br />
<br />
134.<br />
三个囚犯被送到了古拉格。他们开始谈论为什么他们会来到这里。<br />
“我来到这儿，是因为我上班总是迟到五分钟，他们指控我是在以怠工来破坏。”第一个说。<br />
“我来到这儿，是因为我上班总是早到五分钟，他们指控我是间谍”，第二个说。<br />
“我来到这儿，是因为我上班总是准时赶到，”第三个说，”他们指控我拥有一个西方制的手表。” <br />
<br />
135.<br />
来了一个格鲁吉亚代表团。他们被斯大林接见，谈话，然后离开，斯大林开始找他的烟斗，找不到。他叫贝利亚来（秘密警察头子）。<br />
“追代表团，找找谁拿了我的烟斗。”他说。<br />
贝利亚赶忙去追代表团。五分钟后，斯大林在一堆纸下找到了他的烟斗。他叫贝利：“瞧，我找到我的烟斗了。”<br />
“太晚了，”贝利亚说，”代表团中的半数已经承认他们拿了你的烟斗，另外一半在审讯中死掉了。” <br />
<br />
136.<br />
勃列日涅夫在冬季奥运会上阅读讲话稿：”O-O-O-O-O。”<br />
“不，”他的助理向他低声道，“那是奥运会的标志。” <br />
<br />
137.<br />
苏联生产的彩电常常发生爆炸事故。<br />
一次彩电爆炸事故发生以后，一个现场采访的记者气愤的说：这种彩电应该让敌人看！ <br />
<br />
138.<br />
五一劳动节游行中，一个非常老的犹太人举着一个标语：为了我快乐的童年时代，谢谢你，斯大林同志。<br />
党代表找到他：那是什么？你在嘲弄我党吗？谁都可以看出，当年还是孩子的时候，斯大林同志还没有出生。<br />
老人回答：没错，那就是我感谢他的原因。 <br />
<br />
139.<br />
一个男人回家看见他的老婆和一个陌生男人躺在床上。<br />
他吼道：”你这个没用的东西！你看看你都把时间浪费在了什么上面？街角的商店在卖鸡蛋，这会那里只盛下三篮鸡蛋啦！” <br />
<br />
140.<br />
“美国的摩天楼真是全世界最高的吗？”<br />
“是的。但是苏联制造的晶体管是全世界最大的” <br />
<br />
141.<br />
“如果我们放开西部边界管制的话，我们应该做什么？”<br />
“立即跑到西伯利亚以避免被踩死” <br />
<br />
142.<br />
“如果我们突然有了工作欲望怎么办？”<br />
“在沙发上休息一会，你会平静下来的” <br />
<br />
143.<br />
“为什么那个枪击政府豪华轿车的家伙会在红场打偏子弹？”<br />
“因为刚巧挨着他的公民们拼命抢那把枪并叫着：‘让我来射击！’” <br />
<br />
144.<br />
“将军的儿子能成为元帅吗？”<br />
“不可能。元帅都有儿子” <br />
<br />
145.<br />
“为什么我国政府不急着登月？”<br />
“如果宇航员拒绝回来怎么办？” <br />
<br />
146.<br />
“是中国茶还是苏联茶好喝？”<br />
“别掺和中苏论战。喝咖啡吧” <br />
<br />
147.<br />
“尼克松访问莫斯科的时候和赫鲁晓夫绕着克林木林宫比赛跑步，尼克松赢了。我们的媒体会怎么报道？”<br />
“在一场国际跑步竞赛中苏共总书记光荣地获得亚军，而尼克松先生则是副班长。” <br />
<br />
148.<br />
“第三次世界大战会发生吗？”<br />
“不会。不过争取和平的努力会达到这样的程度：全世界都不会有一块石头保持完整” <br />
<br />
149.<br />
“为什么巡警总是三人一队？”<br />
“这是专门的安排。一个会阅读，一个会写字，自然第三个要随时监视这两个知识分子” <br />
<br />
150.<br />
有人发明一部神奇电话，想打给谁就打给谁。<br />
打电话到天堂，找列宁同志，话务员说：他不在这里！<br />
他又打电话到地狱，找到了列宁同志，向他汇报了国际形势，谈了一小时。<br />
第二天他收到账单，打给天堂的电话100块，打给地狱的才10块。<br />
问怎么回事？ 电话局回答：你们打给天堂是长途，打给地狱的算市话。 <br />
<br />
151.<br />
有人通过走关系，把列宁同志送到了天堂，这个无神论者被接纳了。<br />
过了几天，这人给上帝打电话表示感谢，电话接通了，他说：你是上帝吗?<br />
上帝回答：首先，没有上帝。其次，我们这里每个人都是同志。第三，有事请直说，我还要参加党代表会议。<br />
<br />
152.<br />
大西洋上某处，苏联和美国核潜艇碰巧在一处上浮出水。　<br />
苏联潜艇的船员在甲板上没秩序地站着队，半醉的舰长生气地吼道：谁把靴子扔到控制台上去啦？我再问一遍：谁他妈把靴子扔到控制台上去啦？<br />
刚好美国潜艇甲板上的舰长听见了，与苏联舰长相比，他不仅刮了胡子而且精神抖擞。而他的手下更是秩序井然。<br />
显然他很自豪。他说：呵呵，这要是在我们美国――<br />
苏联舰长不耐烦地打断他：行啦，没有什么美国啦――我再问一遍，谁他妈把靴子扔到控制台上去啦？！</div>

							
							
							
							
