&nbsp; &nbsp; 拉宾诺维奇回答道：我和总路线一起动摇。<br />
<br />
&nbsp; &nbsp; 15.<br />
&nbsp; &nbsp; “拉宾诺维奇，你经常读报纸吗？ ”<br />
&nbsp; &nbsp; “当然，要不我怎么知道我们过着幸福的生活？”<br />
<br />
&nbsp; &nbsp; 16.<br />
&nbsp; &nbsp; “共产主义还有会货币吗？”<br />
&nbsp; &nbsp; “南斯拉夫的修正主义者说会有，中国的教条主义者说没有，我们是辩证地看待这个问题――有，但不是所有人都有。”<br />
<br />
&nbsp; &nbsp; 17.<br />
&nbsp; &nbsp; 伊万看电视，是列昂尼德同志在演讲。伊万觉得无聊，换了一个，还是列昂尼德同志在演讲，又换一个，还是他。伊万 一连拨了几十个台，最后累了，准备关电视。这时候电视画面变成了KGB的尤里同志，怒气冲冲地叫：你再敢换？再敢换？再换判你10年大牢！<br />
<br />
&nbsp; &nbsp; 18.<br />
&nbsp; &nbsp; 一个美国人和一个苏联人谈话.<br />
&nbsp; &nbsp; 美国人：我敢在白宫外面大喊，里根下台，你敢吗?<br />
&nbsp; &nbsp; 苏联人：有什么不敢的?<br />
&nbsp; &nbsp; 说完，苏联人走到克里姆林宫外，大喊：里根下台!<br />
<br />
&nbsp; &nbsp; 19.<br />
&nbsp; &nbsp; 拉宾诺维奇到资本主义国家出差，在那里，他给单位拍了一个电报：我选择了自由。<br />
&nbsp; &nbsp; 这事发生后，单位马上召集了党代会谴责拉宾诺维奇，并要做出组织结论。在会议中间，拉宾诺维奇突然走进了会场！全场哑然。<br />
&nbsp; &nbsp; 拉宾诺维奇说道：我非常感兴趣，你们是怎样理解自由的。<br />
<br />
&nbsp; &nbsp; 20.<br />
&nbsp; &nbsp; “苏联人第六感的什么方面最发达？ ”<br />
&nbsp; &nbsp; “高度的满足感。”<br />
<br />
&nbsp; &nbsp; 21.<br />
&nbsp; &nbsp; “为什么我们执行总路线那么慢? ”<br />
&nbsp; &nbsp; “因为每次党代会转的弯太大。”<br />
<br />
&nbsp; &nbsp; 22.<br />
&nbsp; &nbsp; 二战结束后一个波兰农村老太看到街上贴着的照片就说像自己的外甥。<br />
&nbsp; &nbsp; 别人训斥她：瞎说什么，这是斯大林同志<br />
&nbsp; &nbsp; 老太：他是干什么的？<br />
&nbsp; &nbsp; 答：他赶跑了纳粹。<br />
&nbsp; &nbsp; 老太急切地问：他能不能把俄国人也赶跑啊？<br />
<br />
&nbsp; &nbsp; 23.<br />
&nbsp; &nbsp; 一老者人行道闲溜，不慎落入道旁河中，高呼救命。<br />
&nbsp; &nbsp; 两警察闻之，视若不见，仍边走边谈笑如旧。<br />
&nbsp; &nbsp; 老者情急生智，随又高呼：打倒勃列日涅夫！<br />
&nbsp; &nbsp; 两警察闻之大惊，急速跳入河中，将老者拖上岸来铐之。<br />
