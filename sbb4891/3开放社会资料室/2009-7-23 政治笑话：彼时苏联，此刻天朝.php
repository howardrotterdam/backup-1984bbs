&nbsp; &nbsp; 斯大林在大会上引经据典地说：”马克思和列宁说1+1=2，而托洛茨基和布哈林说1+1不等于3。是托洛茨基和布哈林说的对呢？还是马克思和列宁说得对呢？”下面听众一脸疑惑。<br />
&nbsp; &nbsp; “毫无疑问，是马克思和列宁说的对！”底下热烈鼓掌，“托洛茨基和布哈林是帝国主义派来的间谍，说1+1不等于 3的人罪不容赦……”<br />
<br />
&nbsp; &nbsp; 129.<br />
&nbsp; &nbsp; 当年的捷克斯洛伐克政府中，设立了一个海军部，结果，苏联老大哥就对捷克人说：你们是内陆国家，设什么海军部？<br />
&nbsp; &nbsp; 捷克人回答说：那你们不是也设了文化部吗？<br />
<br />
&nbsp; &nbsp; 130.<br />
&nbsp; &nbsp; 勃列日涅夫：同志们，美国人登上了月球，我们不能再等了，党决定让你们上太阳。<br />
&nbsp; &nbsp; 宇航员：总书记同志，我们会被烧死的。<br />
&nbsp; &nbsp; 勃列日涅夫：没关系，同志们，党都替你们想好了，你们晚上去。<br />
<br />
&nbsp; &nbsp; 131.<br />
&nbsp; &nbsp; 二次大战时，罗斯福，丘吉尔，及斯大林三位巨头在雅尔塔开会。罗斯福，丘吉尔处处被动，因此两人合计着想算计斯大林一把。<br />
&nbsp; &nbsp; 丘吉尔说：“昨天我作了一个梦，梦见自己成了地球的主宰！”<br />
&nbsp; &nbsp; 罗斯福也接着说：“昨天我也作了一个梦，梦见自己成了宇宙的主宰！”<br />
&nbsp; &nbsp; 两人说完后，得意地瞧着斯大林。斯大林微微一笑，说道：“昨天我也作了 一个梦，梦见自己既没有批准对丘吉尔先生的任命，也没有批准对罗斯福先生的任命。”<br />
<br />
&nbsp; &nbsp; 132.<br />
&nbsp; &nbsp; 两男一女在沉船后沦落荒岛。<br />
&nbsp; &nbsp; 如果两个男的是西班牙人，他们会用决斗来决定谁拥有那个女人。<br />
&nbsp; &nbsp; 如果他们是法国人，一个是女人的丈夫，另一个是她的情人。 　<br />
&nbsp; &nbsp; 如果他们是苏联人，则会把报告书塞入瓶子，让水流把它漂回祖国，请上级指示他们谁可以娶她为妻。<br />
<br />
&nbsp; &nbsp; 133.<br />
&nbsp; &nbsp; 一个美国人刚从莫斯科机场出来，他想打听一下时间。这时从身旁走过一位莫斯科人，手里拎着个沉重的箱子，很吃力的样子。美国人拦住了他，问他当地时间。莫斯科人放下箱子，抬手看了看表，一口气报出来现在的时间然后还有气压温度湿度等信息。美国人很吃惊，问道：这块表的功能可真多，是苏联的技术生产的吗？<br />
&nbsp; &nbsp; 莫斯科人自豪地回答：是的，是我们苏联技术制造的。<br />
&nbsp; &nbsp; 然后又吃力地重新拎起那个箱子接着说道：就是电池重了点。<br />
<br />
&nbsp; &nbsp; 134.<br />
&nbsp; &nbsp; 三个囚犯被送到了古拉格。他们开始谈论为什么他们会来到这里。<br />
&nbsp; &nbsp; “我来到这儿，是因为我上班总是迟到五分钟，他们指控我是在以怠工来破坏。”第一个说。<br />
&nbsp; &nbsp; “我来到这儿，是因为我上班总是早到五分钟，他们指控我是间谍”，第二个说。<br />
&nbsp; &nbsp; “我来到这儿，是因为我上班总是准时赶到，”第三个说，”他们指控我拥有一个西方制的手表。”<br />
<br />
&nbsp; &nbsp; 135.<br />
&nbsp; &nbsp; 来了一个格鲁吉亚代表团。他们被斯大林接见，谈话，然后离开，斯大林开始找他的烟斗，找不到。他叫贝利亚来（秘密警察头子）。<br />
&nbsp; &nbsp; “追代表团，找找谁拿了我的烟斗。”他说。<br />
&nbsp; &nbsp; 贝利亚赶忙去追代表团。五分钟后，斯大林在一堆纸下找到了他的烟斗。他叫贝利：“瞧，我找到我的烟斗了。”<br />
&nbsp; &nbsp; “太晚了，”贝利亚说，”代表团中的半数已经承认他们拿了你的烟斗，另外一半在审讯中死掉了。”<br />
<br />
&nbsp; &nbsp; 136.<br />
&nbsp; &nbsp; 勃列日涅夫在冬季奥运会上阅读讲话稿：”O-O-O-O-O。”<br />
&nbsp; &nbsp; “不，”他的助理向他低声道，“那是奥运会的标志。”<br />
<br />
&nbsp; &nbsp; 137.<br />
&nbsp; &nbsp; 苏联生产的彩电常常发生爆炸事故。<br />
&nbsp; &nbsp; 一次彩电爆炸事故发生以后，一个现场采访的记者气愤的说：这种彩电应该让敌人看！<br />
<br />
&nbsp; &nbsp; 138.<br />
&nbsp; &nbsp; 五一劳动节游行中，一个非常老的犹太人举着一个标语：为了我快乐的童年时代，谢谢你，斯大林同志。<br />
&nbsp; &nbsp; 党代表找到他：那是什么？你在嘲弄我党吗？谁都可以看出，当年还是孩子的时候，斯大林同志还没有出生。<br />
&nbsp; &nbsp; 老人回答：没错，那就是我感谢他的原因。<br />
<br />
&nbsp; &nbsp; 139.<br />
&nbsp; &nbsp; 一个男人回家看见他的老婆和一个陌生男人躺在床上。<br />
&nbsp; &nbsp; 他吼道：”你这个没用的东西！你看看你都把时间浪费在了什么上面？街角的商店在卖鸡蛋，这会那里只盛下三篮鸡蛋啦！”<br />
<br />
&nbsp; &nbsp; 140.<br />
&nbsp; &nbsp; “美国的摩天楼真是全世界最高的吗？”<br />
&nbsp; &nbsp; “是的。但是苏联制造的晶体管是全世界最大的”<br />
<br />
&nbsp; &nbsp; 141.<br />
&nbsp; &nbsp; “如果我们放开西部边界管制的话，我们应该做什么？”<br />
&nbsp; &nbsp; “立即跑到西伯利亚以避免被踩死”<br />
<br />
&nbsp; &nbsp; 142.<br />
&nbsp; &nbsp; “如果我们突然有了工作欲望怎么办？”<br />
&nbsp; &nbsp; “在沙发上休息一会，你会平静下来的”<br />
<br />
&nbsp; &nbsp; 143.<br />
&nbsp; &nbsp; “为什么那个枪击政府豪华轿车的家伙会在红场打偏子弹？”<br />
&nbsp; &nbsp; “因为刚巧挨着他的公民们拼命抢那把枪并叫着：‘让我来射击！’”<br />
<br />
&nbsp; &nbsp; 144.<br />
&nbsp; &nbsp; “将军的儿子能成为元帅吗？”<br />
&nbsp; &nbsp; “不可能。元帅都有儿子”<br />
<br />
&nbsp; &nbsp; 145.<br />
&nbsp; &nbsp; “为什么我国政府不急着登月？”<br />
&nbsp; &nbsp; “如果宇航员拒绝回来怎么办？”<br />
<br />
&nbsp; &nbsp; 146.<br />
&nbsp; &nbsp; “是中国茶还是苏联茶好喝？”<br />
&nbsp; &nbsp; “别掺和中苏论战。喝咖啡吧”<br />
<br />
&nbsp; &nbsp; 147.<br />
&nbsp; &nbsp; “尼克松访问莫斯科的时候和赫鲁晓夫绕着克林木林宫比赛跑步，尼克松赢了。我们的媒体会怎么报道？”<br />
&nbsp; &nbsp; “在一场国际跑步竞赛中苏共总书记光荣地获得亚军，而尼克松先生则是副班长。”<br />
<br />
&nbsp; &nbsp; 148.<br />
&nbsp; &nbsp; “第三次世界大战会发生吗？”<br />
&nbsp; &nbsp; “不会。不过争取和平的努力会达到这样的程度：全世界都不会有一块石头保持完整”<br />
<br />
&nbsp; &nbsp; 149.<br />
&nbsp; &nbsp; “为什么巡警总是三人一队？”<br />
&nbsp; &nbsp; “这是专门的安排。一个会阅读，一个会写字，自然第三个要随时监视这两个知识分子”<br />
<br />
&nbsp; &nbsp; 150.<br />
&nbsp; &nbsp; 有人发明一部神奇电话，想打给谁就打给谁。<br />
&nbsp; &nbsp; 打电话到天堂，找列宁同志，话务员说：他不在这里！<br />
&nbsp; &nbsp; 他又打电话到地狱，找到了列宁同志，向他汇报了国际形势，谈了一小时。<br />
&nbsp; &nbsp; 第二天他收到账单，打给天堂的电话100块，打给地狱的才10块。<br />
&nbsp; &nbsp; 问怎么回事？ 电话局回答：你们打给天堂是长途，打给地狱的算市话。<br />
<br />
&nbsp; &nbsp; 151.<br />
&nbsp; &nbsp; 有人通过走关系，把列宁同志送到了天堂，这个无神论者被接纳了。<br />
&nbsp; &nbsp; 过了几天，这人给上帝打电话表示感谢，电话接通了，他说：你是上帝吗?<br />
&nbsp; &nbsp; 上帝回答：首先，没有上帝。其次，我们这里每个人都是同志。第三，有事请直说，我还要参加党代表会议。<br />
<br />
&nbsp; &nbsp; 152.<br />
&nbsp; &nbsp; 大西洋上某处，苏联和美国核潜艇碰巧在一处上浮出水。　<br />
&nbsp; &nbsp; 苏联潜艇的船员在甲板上没秩序地站着队，半醉的舰长生气地吼道：谁把靴子扔到控制台上去啦？我再问一遍：谁他妈把靴子扔到控制台上去啦？<br />
&nbsp; &nbsp; 刚好美国潜艇甲板上的舰长听见了，与苏联舰长相比，他不仅刮了胡子而且精神抖擞。而他的手下更是秩序井然。<br />
&nbsp; &nbsp; 显然他很自豪。他说：呵呵，这要是在我们美国――<br />
&nbsp; &nbsp; 苏联舰长不耐烦地打断他：行啦，没有什么美国啦――我再问一遍，谁他妈把靴子扔到控制台上去啦？！</div>

							
							
							
							
