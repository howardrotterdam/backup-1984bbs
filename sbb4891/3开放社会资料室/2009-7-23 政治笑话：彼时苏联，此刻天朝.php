													<h2>苏联政治笑话―转自东东枪的博客</h2>
						
						
																											<div id="postmessage_180109" class="t_msgfont"><a href="http://thisisdongdongqiang.cn/" target="_blank">http://thisisdongdongqiang.cn/</a><br />
1.<br />
亚历山大，凯撤，拿破仑做为贵宾，参加红场阅兵。<br />
“我要是有苏联的坦克，我将是战无不胜的！”亚历山大说。<br />
“我要是有苏联的飞机，我将征服全世界！”凯撒说。<br />
“我要是有真理报，世界现在也不会知道滑铁卢！”拿破仑说。 <br />
<br />
2.<br />
在苏共二十三次代表大会上，勃列日涅夫作报告。他问：”我们这里有没有敌人？”<br />
一个人回答：”有一个，他坐在第四排第十八号位子上。”<br />
勃问：”为什么他是敌人？”<br />
回答：”列宁说过敌人是不会打瞌睡的，我发现全场只有他一个人没有打瞌睡！” <br />
<br />
3.<br />
在集体农庄，党的会议上将研究两个问题：建造木棚和建设共产主义。<br />
但是，在没有木板的情况下，决定直接研究第二个问题。 <br />
<br />
4.<br />
“苏联和英国的童话传说有什么不同？”<br />
“英国童话的开头通常是：‘很久很久以前……’而我们的则是；‘不远了，不远了……’ “<br />
<br />
5.<br />
斯大林做报告说：”共产主义已经出现在苏联的地平线上了……”<br />
老工人不知道什么是地平线，回家后问儿子，儿子说：”地平线就是能看到却永远走不到的一条线。”<br />
<br />
6.<br />
“同志，你对这个问题有什么意见吗？”党支书问道。<br />
“对，我是有意见，但我不同意我的意见！” <br />
<br />
7.<br />
勃烈日涅夫同志当上苏共中央总书记之后，将在乡下的老母亲接到了莫斯科。老太太来了以后，勃列日涅夫得意洋洋地向老妈展示了一番自己的豪华别墅，高级汽车，名贵家具等等，展示完了后，勃列日涅夫问老太太这一切如何。老太太说：”儿子啊，这一切都很好，但是，共产党来了你怎么办？”<br />
<br />
8.<br />
两个中学同学相遇然后寒暄。<br />
“您现在在哪工作”<br />
“中学老师.您呢?”<br />
“克格勃。”<br />
“啊，您在克格勃具体干什么？”<br />
“我们负责揪出那些对国家不满的家伙。”<br />
“您的意思是……还有人比较满意？”<br />
“哦，那些人不归我们管，负责管他们的是纪委。” <br />
<br />
9.<br />
工人说：我们已经是共产主义了。<br />
有人问：为什么？<br />
工人说：我们实现了各尽所能，各取所需的分配原则。<br />
什么？那人又问。<br />
工人说：你没见吗？我们的领导各取所需，工人各尽所能! <br />
<br />
10.<br />
“在苏联是否可以存在两党制？”<br />
“不，不可能，我们养不起。” <br />
<br />
11.<br />
“什么在苏联是最常见的？”<br />
“暂时的困难。”<br />
<br />
12.<br />
“苏联制度的优越性在哪里？”<br />
“成功地克服了在其它社会制度里不会存在的困难。”<br />
<br />
13.<br />
苏联时期莫斯科卢比扬卡（克格勃总部）大楼外。<br />
一个愁眉苦脸的男子一边走路一边自言自语：肥皂没有，电池没有，袜子也没有……<br />
这时旁边走过来一个看起来象是便衣的人小声对他说：公民同志，您要是再这样诋毁我们伟大的社会主义国家，我就要拿手枪把敲你的脑袋了！<br />
那个男子看看便衣警察，继续自言自语：看看，连子弹也没有…… <br />
<br />
14.<br />
在调查表上有这样一个问题：在执行总路线时你动摇过吗？<br />
拉宾诺维奇回答道：我和总路线一起动摇。 <br />
<br />
15.<br />
“拉宾诺维奇，你经常读报纸吗？ ”<br />
“当然，要不我怎么知道我们过着幸福的生活？”<br />
<br />
16.<br />
“共产主义还有会货币吗？”<br />
“南斯拉夫的修正主义者说会有，中国的教条主义者说没有，我们是辩证地看待这个问题――有，但不是所有人都有。”<br />
<br />
17.<br />
伊万看电视，是列昂尼德同志在演讲。伊万觉得无聊，换了一个，还是列昂尼德同志在演讲，又换一个，还是他。伊万 一连拨了几十个台，最后累了，准备关电视。这时候电视画面变成了KGB的尤里同志，怒气冲冲地叫：你再敢换？再敢换？再换判你10年大牢！<br />
<br />
18.<br />
一个美国人和一个苏联人谈话.<br />
美国人：我敢在白宫外面大喊，里根下台，你敢吗?<br />
苏联人：有什么不敢的?<br />
说完，苏联人走到克里姆林宫外，大喊：里根下台! <br />
<br />
19.<br />
拉宾诺维奇到资本主义国家出差，在那里，他给单位拍了一个电报：我选择了自由。<br />
这事发生后，单位马上召集了党代会谴责拉宾诺维奇，并要做出组织结论。在会议中间，拉宾诺维奇突然走进了会场！全场哑然。<br />
拉宾诺维奇说道：我非常感兴趣，你们是怎样理解自由的。 <br />
<br />
20.<br />
“苏联人第六感的什么方面最发达？ ”<br />
“高度的满足感。”<br />
<br />
21.<br />
“为什么我们执行总路线那么慢? ”<br />
“因为每次党代会转的弯太大。” <br />
<br />
22.<br />
二战结束后一个波兰农村老太看到街上贴着的照片就说像自己的外甥。<br />
别人训斥她：瞎说什么，这是斯大林同志<br />
老太：他是干什么的？<br />
答：他赶跑了纳粹。<br />
老太急切地问：他能不能把俄国人也赶跑啊？<br />
<br />
23.<br />
一老者人行道闲溜，不慎落入道旁河中，高呼救命。<br />
两警察闻之，视若不见，仍边走边谈笑如旧。<br />
老者情急生智，随又高呼：打倒勃列日涅夫！<br />
两警察闻之大惊，急速跳入河中，将老者拖上岸来铐之。 <br />
<br />
24.<br />
在苏联的一次大会上，主持人突然说：下面请认为社会主义好的同志坐到会场的左边，认为资本主义好的同志坐到会场右边。<br />
大部分人坐到了左边，少数人坐到右边，只有一个人还坐在中间不动。<br />
主持人：那位同志，你到底认为社会主义好还是资本主义好？<br />
回答：我认为社会主义好，但是我的生活像是资本主义。<br />
主持人慌忙说：那请您赶快坐到主席台上来。 <br />
<br />
25.<br />
“有 40 颗牙4条腿的是什么东西？”<br />
“鳄鱼。”<br />
“有4颗牙40 条腿的是什么东西？”<br />
“政治局。” <br />
<br />
26.<br />
“假设你在酒吧里，而一个陌生人坐到你的身边并开始唉声叹气，你该怎么做？”<br />
“立即去阻止这种反苏宣传。” <br />
<br />
27.<br />
“共产主义实现时警察还存在吗？”<br />
“当然不。那时候所有公民都已经学会自己逮捕自己” <br />
<br />
28.<br />
勃列日涅夫访问印度时，大批印度人到机场欢迎勃列日涅夫。勃列日涅夫便问当时的印度总理英甘地，你是用什么办法让这么多人来欢迎我的？英甘地回答：凡是来欢迎你的人，都能够得到5个卢比（印度货币）的奖励。<br />
后来英甘地到苏联访问时，成千上万的苏联人从机场到莫斯科市内的道路上夹道欢迎。英甘地问勃列日涅夫：你是用什么办法让这么多人来欢迎我的？勃列日涅夫回答：凡是不来欢迎你的莫斯科人，每人罚款5卢布（苏联的货币） <br />
<br />
29.<br />
一个苏联人家中总是被偷，于是很郁闷的问邻居：“什么时候我们家的东西才能不被偷？”<br />
邻居说：“等到了共产主义社会就不被偷了。”<br />
“为什么？”<br />
“因为在社会主义阶段就已经被偷光了。” <br />
<br />
30.<br />
“亚当和夏娃是哪国人？”<br />
“他们没衣服穿，分享一只苹果，还觉得自己活在天堂里，当然是苏联人。”<br />
<br />
31.<br />
“你为什么坐牢？”<br />
“我反对伊万诺维奇。”<br />
“你又为什么坐牢？ ”<br />
“我支持伊万诺维奇。”<br />
“你呢？”<br />
“我是伊万诺维奇。” <br />
<br />
32.<br />
苏联特工对美国特工夸口道：“我们知道你们国家所有核导弹发射井的位置。”<br />
美国特工说：“那有什么，我们知道你们未来20年全体政治局委员名单。” <br />
<br />
33.<br />
问：共产主义是科学还是艺术？<br />
答：当然是艺术，如果是科学就应该先用小白鼠做实验。 <br />
<br />
34.<br />
亚美亚尼电台播放着机智问答节目，主持人问：鸡和蛋哪一个先有？<br />
解答者在一旁暗暗地答道：以前两样都有。 <br />
<br />
35.<br />
在苏联阅兵式上，步兵，炮兵，装甲兵，战略导弹部队依次出场，但在游行最后却走出几个西装革履的人，一个旁边观看的人问：“这几个人是谁？”<br />
另一个人回答：“计划经济委员会的。” <br />
<br />
36.<br />
一对夫妇生了一个孩子，如果长得像父母，那就是按反动的基因学说生出来的。如果长得像他们的邻居，那就是按照李森科的革命的环境决定学说生出来的。 <br />
<br />
37.<br />
戈尔巴乔夫颁布限酒令后，想要买到酒只能在指定销售处排长队来买。<br />
一个莫斯科人排队排烦了，就嚷道：“我不排了！我要到克林姆林宫把戈尔巴乔夫干掉！”然后转身离去。<br />
一会儿他又回来了，旁边人问他：”已经干掉了吗？”<br />
他说：“干什么干啊！那儿的队比这儿还长。” <br />
<br />
38.<br />
一天，戈尔巴乔夫到一个农场视察，看到有几头猪，就到猪旁边拍了一张照片。<br />
随行人员在照片上写上：戈尔巴乔夫和猪在一起。<br />
但又感到不妥。于是改成：猪和戈尔巴乔夫在一起。<br />
还是感觉不对。最后把照片上的字改成：左起第三位是戈尔巴乔夫。 <br />
<br />
39.<br />
莫斯科地铁中。<br />
“公民您好。”<br />
