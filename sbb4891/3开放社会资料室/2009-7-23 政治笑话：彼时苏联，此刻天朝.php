&nbsp; &nbsp; “我们负责揪出那些对国家不满的家伙。”<br />
&nbsp; &nbsp; “您的意思是……还有人比较满意？”<br />
&nbsp; &nbsp; “哦，那些人不归我们管，负责管他们的是纪委。”<br />
<br />
&nbsp; &nbsp; 9.<br />
&nbsp; &nbsp; 工人说：我们已经是共产主义了。<br />
&nbsp; &nbsp; 有人问：为什么？<br />
&nbsp; &nbsp; 工人说：我们实现了各尽所能，各取所需的分配原则。<br />
&nbsp; &nbsp; 什么？那人又问。<br />
&nbsp; &nbsp; 工人说：你没见吗？我们的领导各取所需，工人各尽所能!<br />
<br />
&nbsp; &nbsp; 10.<br />
&nbsp; &nbsp; “在苏联是否可以存在两党制？”<br />
&nbsp; &nbsp; “不，不可能，我们养不起。”<br />
<br />
&nbsp; &nbsp; 11.<br />
&nbsp; &nbsp; “什么在苏联是最常见的？”<br />
&nbsp; &nbsp; “暂时的困难。”<br />
<br />
&nbsp; &nbsp; 12.<br />
&nbsp; &nbsp; “苏联制度的优越性在哪里？”<br />
&nbsp; &nbsp; “成功地克服了在其它社会制度里不会存在的困难。”<br />
<br />
&nbsp; &nbsp; 13.<br />
&nbsp; &nbsp; 苏联时期莫斯科卢比扬卡（克格勃总部）大楼外。<br />
&nbsp; &nbsp; 一个愁眉苦脸的男子一边走路一边自言自语：肥皂没有，电池没有，袜子也没有……<br />
&nbsp; &nbsp; 这时旁边走过来一个看起来象是便衣的人小声对他说：公民同志，您要是再这样诋毁我们伟大的社会主义国家，我就要拿手枪把敲你的脑袋了！<br />
&nbsp; &nbsp; 那个男子看看便衣警察，继续自言自语：看看，连子弹也没有……<br />
<br />
