													<h2>我以个人的经验推荐几个下书的好去处</h2>
						
						
																											<div id="postmessage_316992" class="t_msgfont">喜欢读电子书的人都知道52ebook，可是自从52ebook被勒令整顿之后，读友们又该从哪下载免费的图书资源呢。52ebook 一直升级维护中<a href="http://www.52ebook.com" target="_blank">http://www.52ebook.com</a><br />
　　<br />
　　下面我以个人的经验推荐几个下书的好去处：<br />
　　<br />
　　1.霓裳小轩。（首推）&nbsp;&nbsp;<a href="http://www.emgog.com/bbs/?fromuid=3569" target="_blank">http://www.emgog.com/bbs/?fromuid=3569</a>&nbsp;&nbsp;<br />
版主招募了52ebook的部分高手，整个论坛都是52ebook的大合集，书籍极多。 <br />
　　原来52Ebook暂停了之后，据说大多人员便集中到这了。<br />
　　要注册，下书要论坛的虚拟币。这种虚拟币的得来倒也不是太难，回个贴就有一个币，比如说你要下载某个附件，只要在这帖子后回个贴也就下来了。当然，有些书箱附件特别多，比如说《汉译世界学术名著丛书》，就有十多个附件。平时不积累得币币遇到好书就只好作罢了。<br />
　　<br />
　　尽管论坛的虚拟币不难得，可是倒是挺让新人恼火的。记得我刚注册的时候，进去一看，好兴奋，哇，书挺多嘛，而且好多是没收藏的。所以二话没说就要下载，一下载却提示没有足够的虚拟币，热情立马冷了半截。有些单本的书还比较好办，作成电子书容量不大，一个附件下载，这只要一个虚拟币，我回个贴就OK了。要是碰到作家全集，比如《鲁迅全集》，10多个附件，这就要让我骂人了。<br />
　　<br />
　　霓裳小轩的书是比较全的，而且制作精良。我刚注册不久，要传书赚点虚拟币，感觉挺为难。只要我想得起来的作家作品，上去一搜，发现早有了，而重复发贴传书是要被删的。我一开始不知道，花了好久传了套《鲁迅全集》，隔天就被删掉了，郁闷死。现在我发贴大多找学术性的书，这种书少人看，自然少人发，而我发上去，版主要考虑论坛的全面性，又不会删除，让我赚了不少便宜。<br />
　　<br />
　　论坛地址：<a href="http://www.emgog.com/bbs/?fromuid=24431" target="_blank">http://www.emgog.com/bbs/?fromuid=24431</a><br />
　　<br />
　　2.我不知道论坛。 <br />
　　<br />
　　也挺强势的。也要注册，不过比之霓裳小轩，觉得我不知道论坛书不太全面，当然它也有它的优势，就是下载门槛低。就我个人经验，我刚注册后就在论坛里翻箱倒柜地下载，下了几百MB的书，好像也没碰到什么障碍。大概也是要先回几贴，冲点威望才能畅通。好在我有个好习惯，就是下了人家书一定回贴说声谢谢，所以没觉得。<br />
　　<br />
　　我不知道论坛和霓裳小轩其实很类似，里头许多书都是以前52Ebook的LOGO。另外我怀疑同时注册这两个论坛的人一定大有人在，比如我，哈哈。所以，在这两个论坛有搞“走私”的现象。比如说吧，有次，我在我不知道论坛传了套《孙犁作品集》，帖子里加入了不少我个人的阅读感受，结果没过几个小时，就在霓裳小轩里看到了我的帖子的翻版。我一笑。<br />
　　<br />
　　论坛地址：<a href="http://bbs.wbzd.net/index.php" target="_blank">http://bbs.wbzd.net/index.php</a><br />
　　<br />
　　3.百灵书库<br />
　　<br />
　　这是百灵社区下的图书分享版块。<br />
　　<br />
　　霓裳小轩和我不知道论坛，以EXE格式的电子书居多。而百灵书库感觉是以PDF格式的书多。EXE书制作精灵，看起来舒服。而PDF则阅读不太方便，而且容量大。不过，PDF有个好处，用起来放心，因为用EXE书我老担心被人种毒，而PDF好像种毒的可能性更少。不知道有没有道理。<br />
　　<br />
　　霓裳小轩和我不知道论坛能满足普通读书人的需求，一般人想找的小说啊，不管是时下流行的还是经典的，大概都能找到。而百灵书库，则深层次点。学术性强的书较多，看了大受启发。我在这里下了不少学术著作和历史性书籍。<br />
　　<br />
　　也要注册。下载有各别高端敏感书有积分限制。特别投诉的是，我感觉百灵的广告太多了。下载个书，还要另外弹出个广告页面，真麻烦。而且，我不知道是我的电脑问题，还是他们系统问题，有些分卷的压缩文件命名混乱，我要自己重新按123命名才能解压。<br />
　　<br />
　　论坛地址：<a href="http://club.beelink.com.cn/index.asp?boardid=168" target="_blank">http://club.beelink.com.cn/index.asp?boardid=168</a><br />
　　<br />
　　4.溜达TXT电子书下载论坛<br />
　　<br />
　　如题所示，这论坛的书以TXT格式居多，定位点在于满足用手机看书的读友的需求。我手机没这功能，用电脑看TXT书感觉比PDF更不方便。好的是，这论坛里也有个板块是专门为下载EXE、CHM格式书准备的，虽然只有一个版块，可是麻雀虽小，五脏具全，书倒是挺全的，能满足一般求书。要注册。<br />
　　<br />
　　论坛地址：<a href="http://www.txtku.cn/" target="_blank">http://www.txtku.cn/</a><br />
　　<br />
　　5.新浪爱问共享资料<br />
　　<br />
　　大众化传书搜书下书网页。资源多，书够全，五花八门什么书什么格式的书都有，从EXE、CHM、到PDF、再到DOC、TXT全面覆盖，肤浅的像生活小技巧到学术专著都有。都是网友自己传上来的。如果不注册可不可以下载，这个不详，因为我一早就有号了。下载有的需用积分，有的不用。要多少积分，没有统一规定，看传书者自己的定位。因为传的人多，可以选择哪些不需要积分的下。<br />
　　<br />
　　书是全的。可是有两个问题：一、像这种大型网页，特别招眼，监管得特别厉害，敏感书籍会被和谐掉。二、因为谁都可以传，所以质量上良莠不齐，有制作好的，也有差的，需要下载者自己甄别。这点上，霓裳小轩和我不知道论坛做得比较好，很少重复资源，而留下来的都是制作精良的。<br />
　　<br />
　　地址：<a href="http://ishare.iask.sina.com.cn/" target="_blank">http://ishare.iask.sina.com.cn/</a><br />
