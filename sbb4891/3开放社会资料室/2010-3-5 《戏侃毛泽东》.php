																											<div id="postmessage_503363" class="t_msgfont">毛诗新解之二：　　沁园春 雪&nbsp;&nbsp;<br />
<br />
上一页 目录页 下一页 <br />
 <br />
 <br />
　　北国风光，千里冰封，万里雪飘。<br />
<br />
　　望长城内外，惟余莽莽；大河上下，顿失滔滔。<br />
<br />
　　山舞银蛇，原驰蜡象，欲与天公试比高。<br />
<br />
　　须晴日，看红装素裹，分外妖娆。<br />
<br />
　　江山如此多娇，引无数英雄竞折腰。<br />
<br />
　　惜秦皇汉武，略输文采；唐宗宋祖，稍逊风骚。<br />
<br />
　　一代天骄，成吉思汗，只识弯弓射大雕。<br />
<br />
　　俱往矣，数风流人物，还看今朝。<br />
<br />
　　此词作于一九三六年二月，刚在陕北站下脚跟的毛泽东“饱暖思权欲”，面对大好河山做起了“帝王梦”。在这首词中，既看不到对沦陷于日寇铁蹄下东北三省的思念，也没有对苏区根据地丧失和数十万生灵涂炭的反省，充斥字里行间的除了统治天下的欲望，便是对历代皇帝的羡慕和超越他们的野心。<br />
<br />
　　纵观历史，毛的帝王思想实际上早已有之。六十年代时身边的人曾问过毛：“主席您小的时候想过当领袖吗？”毛泽东回答道：“想过，小时候，我就有领袖欲，我父亲让我去放鸭，我赶著一群鸭子，好神气噢。看到一群鸭子在我的驱赶下，噗噗下水，我心里就觉得我是在指挥千军万马呢？”<br />
<br />
　　毛17岁时曾经写过一首诗:“独坐池塘如虎踞，绿杨树下养精神。春来我不先开口，哪个虫儿敢作声。”在这首诗中，毛的霸王之气已经跃然纸上。<br />
<br />
　　早在1928年毛任湘赣，边界特委书记时，他的专断就曾受到非议并一度被称为“独裁书记”。后来也担任过边界特委书记的杨开明就说过：“特委的事，总是书记一个人处理，个人专政，书记独裁，成为边界的通弊。首先泽东为特委书记时，特委就在泽东一个人荷包里。” <br />
<br />
　　毛的专横独断表露在1930―1931年他亲自领导的整肃AB团大镇压中，此事造成红色根据地的严重危机和中共力量的削弱。当时红三军的冯笃权对黄克诚说：“毛泽东过于信用顺从自己的人，对持不同意见的人不能一视同仁，不及朱老总宽厚坦诚。”并举例说，一些品质很坏的人就是因为会顺从，受到毛的重用，被赋予很大的权力，干坏了事情也不被追究。果其不然，这位聪颖过人、毫无过错的何笃才不久就被扣之以AB团的恶名遭到杀害。<br />
<br />
　　张国焘和王明都曾是早期中共的高层领导人,对毛泽东最为了解.直到后来他们离开大陆,在言论上不受毛泽东钳制和威摄,才讲出了一些真心话,对认识毛的实质极有参考价值。<br />
<br />
　　中共党史专家司马璐五、六十年代在香港访问过张国焘，张的论述应是研究中共党史的第一手资料。司马璐问及毛泽东的为人，张国焘回答说：“润之（毛）一天没有登基当皇帝，是不甘心的”。当问及毛泽东的领导方式时张国焘答：“老毛的领导方式可以做到为了达到一个政治目的，不顾一切代价。”（见司马璐《张国焘谈与周恩来毛泽东的恩怨》）。<br />
<br />
　　王明1956年到苏联居住后，写了很多揭发毛泽东的文章。毛在延安时曾向很多人表示他到北京后要做皇帝，王明为此而写过一首诗：皇帝从来是独夫，人民头上坐称孤。北京一到做皇帝，蜕化满腔党性无。诗下标明“1948年11月22日晚于西柏坡”注：“毛泽东这天和我谈话又说:年轻时候看小说，常想做皇帝是多么了不起的事呀！可是不晓得怎么才能做皇帝。现在懂得了。我们不久就要进北京了，一到北京，我不就要做皇帝了吗？”他还说：“既然是国家的最高统治者，本质上不就是皇帝吗？” <br />
<br />
　　常言道诗言志，从毛的诗词中也可看到他的皇帝志向。《沁园春 <br />
<br />
　　雪》1936年后即已流传，当时在毛统治的延安已是一党专政，自会有人颂扬，但是在“国统区”却是招来的不少的批评，说毛泽东有帝王思想。中共的宣传部门权力“驳斥”。今日回过头来再看这首诗词，不仅能够看到毛当时志在帝王，而且有超过以往帝王的雄心。<br />
<br />
　　毛还在1973年写了一首七律，诗题为《读〈封建论〉呈郭老》。诗云：劝君少骂秦始皇，焚坑事件要商量。祖龙虽死业犹在，孔丘名高实秕糠。百代多行秦政治，《十批》不是好文章。熟读唐人《封建论》，莫从子厚返文王。从这首类似打油、平仄不甚和谐的七言律诗来看，毛对郭沫若的《十批判书》、特别是该书对秦始皇的评价是相当不满的。（《毛泽东与著名作家》P73）。<br />
<br />
　　毛一向尊崇秦始皇，他也曾经说他自己是马克思加秦始皇。有人说秦始皇坑了四百多个儒，毛泽东说这不算多，我们超过秦始皇一百倍。如果深究毛泽东的思想渊源，首先就是帝王思想。<br />
<br />
　　以此观之，这首《沁园春 雪》是不是可以这样改呢：<br />
<br />
　　北国风光，千里兵疯，万里血漂。<br />
<br />
　　望长城内外，刀枪莽莽；大河上下，民怨滔滔。<br />
<br />
　　山舞毒蛇，原驰病象，日寇中华试比高。<br />
<br />
　　须睁眼，看哀鸿遍野，断壁残蒿。<br />
<br />
　　江山如此多妖，引无数魔头竞折腰。<br />
<br />
　　惜秦皇汉武，略输文采；唐宗宋祖，稍逊风骚。<br />
<br />
　　一代天骄，成吉思汗，只识弯弓射大雕。<br />
<br />
　　俱过气，论心狠手辣，还看老毛。 <br />
