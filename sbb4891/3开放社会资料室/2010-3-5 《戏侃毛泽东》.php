 <br />
　　毛诗新解 <br />
<br />
　　在毛泽东及其党徒掀起的个人崇拜的狂潮中，其诗词一直被奉为“革命现实主义与革命浪漫主义相结合”的经典之作。尤其是文革时与毛的许多语录一起谱曲传唱，可谓“有井水处必有吟者”，影响之广空前绝后。 <br />
<br />
　　然今以信史观之，毛的诗词除早期几首咏志抒情的之外，多无实事求是之风，而时有虚伪浮夸之嫌。现参照目前已经披露出来的史实作些许剖析，以烛照其深入人心的“光辉形象”，并欢迎方家指正。 <br />
<br />
　　是为序。 <br />
<br />
　　毛诗新解之一：七律 长征　 <br />
<br />
　　红军不怕远征难 万水千山只等闲　<br />
<br />
　　五岭逶迤腾细浪 乌蒙磅礴走泥丸　<br />
<br />
　　金沙水拍云崖暖 大渡桥横铁索寒　<br />
<br />
　　更喜岷山千里雪 三军过后尽开颜 <br />
<br />
　　此诗作于一九三五年十月，如题所示，是写红军长征的。从诗中我们根本看不到“苏维埃政府”在大军压境下分崩离析落荒而逃的窘迫，更看不到数十万红军将士喋血湘江赤水、尸横雪山草地的惨烈，而且回避了反围剿失败的内在原因。 <br />
<br />
　　反围剿之所以归于失败，固然与政府军的步步为营稳扎稳打有关，但深究起来，内部争斗如火v王佐袁文才、整肃AB团等已经大大削弱了红军的战斗力，再加上决策层的勾心斗角和竭泽而渔的打土豪甚至消灭富农等血腥暴力，最后弄得自己没了立足之地，只得借抗日为名辗转北上，以图得到苏联的援救。可怜中央红军出发时三十万之众，历尽千辛万苦到达陕北后只剩下三万疲惫之师。 <br />
<br />
　　而据《毛：不为人知的故事》作者张戎对前苏联解密档案的考证，红军之所以能够死里逃生，一个最重要的原因是蒋介石有意放中共一马。因为那时蒋经国被当作人质扣在斯大林手里，蒋希望用红军和中共的生存换回其子。长征时他完全可以把红军消灭掉，但却没有那样做。长征前夕，蒋就向苏联正式提出要求放回儿子，并用放走红军的一系列步骤和斯大林作交易。长征每个关键时刻，如所谓湘江之战，等红军主力全部渡过后，蒋才封锁湘江；又如听任一、二、四方面军会师，毛到吴起镇当天，蒋即透过各种渠道，向苏联要求放儿子。甚至所谓“飞夺泸定桥”也是假的，张戎查到中央红军办的「红星报」，上面根本没有打仗的记载，勇士过桥后还举行了仪式，个个活蹦乱跳。而政府军方面的电报则显示，守军余松部在红军到达前夕就被命令弃守泸定桥去康定，等于给红军让路。 <br />
<br />
　　而毛率领中央红军与实力数倍于自己的四方面军会师后又怎样了呢？看看张国焘后来的命运就不难明白，无非又上演了一场“火v王伦”的好戏。 <br />
<br />
　　由此看来，毛的这首长征如果这样写可能比较符合事实： <br />
<br />
　　红军无奈远征难 万水千山岂等闲　<br />
<br />
　　五岭逶迤鞋底破 乌蒙陡峭险玩完　<br />
<br />
　　金沙水拍心渐冷 大渡桥通胆不寒　<br />
<br />
　　尸横岷山千里雪 三军过后便翻脸 <br />
