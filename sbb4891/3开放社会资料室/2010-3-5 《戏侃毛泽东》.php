　　毛也做得很光棍，立即就说服朱德退兵，而且以后红军还真的没有再也没有打<br />
<br />
　　过长沙。<br />
<br />
　　老毛前脚撤兵，何健后脚就抓了杨开慧。杨当时火气正大着呢，你老毛打到<br />
<br />
　　长沙了，也不来看我们母子一眼，未免太绝情了。被抓起来以后，杨大概也<br />
<br />
　　想大义凛然一翻，说什么：上级的姓名我知道，下级的姓名我也知道，就是<br />
<br />
　　不能告诉你们之类的话。没想到马上被何健打断了，不，不，我们一不要你<br />
<br />
　　交出地下党的名单，二不让写悔过书，只要在报纸上发表声明，跟毛泽东离<br />
<br />
　　婚，就放你走人。杨开慧是个聪明人儿，一听就明白了：是老毛叫你们来劝<br />
<br />
　　我离婚的是不是？何不住地点头，是啊，是啊，这种负心人，跟他也没意思，<br />
<br />
　　离了算啦。<br />
<br />
　　杨是个心高气傲之人，脾气也倔强得很。让那个负心的陈士美得意洋洋地去<br />
<br />
　　娶小妞？门儿也没有！我就是要拖着你，死也不离婚。反正中央不批准，你<br />
<br />
　　就也结不成婚。<br />
<br />
　　何健也没啥办法，就给老毛捎信，说你老婆是湖南倔骡子，说了死也不离，<br />
<br />
　　你看怎么办吧？老毛回信说什么，人固有一死，为老公的利益而死，就比泰<br />
<br />
　　山还重。不过我那3个儿子希望你要保全什么的。<br />
<br />
　　何健是军阀啊，杀人不眨眼，杀个女人更算不了什么了，就跟杨开慧摊牌了，<br />
<br />
　　不离婚就得死，把老毛的信也给她看了。杨一看毛的亲笔信，自己爱过的人<br />
<br />
　　居然一心要致自己于死地，只觉得万念具灰，这个婚是绝对不离的，活着也<br />
<br />
　　没有意思，但求一死。<br />
<br />
　　信件一来一往，拖了一个来月。相传行刑队的刽子手同情杨的遭遇，故意没<br />
<br />
　　有打中要害。行刑队刚要走，老毛派去的细作在一边喊了起来：“没有打死，<br />
<br />
　　没有打死！” 刽子手无奈才又回来补了一枪。<br />
<br />
　　杨死了以后，何健放了杨的3个儿子，听任老毛把他们接去上海。<br />
<br />
　　蝴蝶款款恋花飞，似乎很浪漫不是？但实际上蝴蝶的前身毛毛虫是靠吃花的<br />
