																											<div id="postmessage_503365" class="t_msgfont">毛诗新解之三：蝶恋花　答李淑一&nbsp;&nbsp;<br />
<br />
上一页 目录页 下一页 <br />
 <br />
 <br />
　　　　　　　　　　　<br />
<br />
　　我失骄杨君失柳，杨柳轻r直上重霄九。 <br />
<br />
　　问讯吴刚何所有，吴刚捧出桂花酒。 <br />
<br />
　　寂寞嫦娥舒广袖，万里长空且为忠魂舞。<br />
<br />
　　忽报人间曾伏虎，泪飞顿作倾盆雨。 <br />
<br />
　　此词作于1957年5月11日，名曰答李淑一，“我失骄杨”实则为怀念前妻杨开慧。<br />
<br />
　　这首词给人的印象是缠绵悱恻情真意切，夫妻恩爱跃然纸上，思念之心力透纸背，读后令人感慨万千。吾亦深感伟大领袖虽然搞阶级斗争和政治运动心狠手辣，内心世界里却还有如此温情的一面，难怪被郭沫若盛赞为杰出的革命浪漫主义作品。<br />
<br />
　　然而随着一些历史真相的浮出水面，再读这首词时又有了新的感受，鄙视和厌恶之情油然而生。<br />
<br />
　　据史料披露：1930年夏军阀何键到处搜杀共产党人及其家属，杨开慧于10月间不幸被捕。她几乎每天都被提去过堂，遭到皮鞭、木棍的毒打，还被压杠子，被打昏后又用凉水泼醒。她回到牢房，和年仅8岁的毛岸英抱在一起，告诉他父亲一定会回来打“坏人”。<br />
<br />
　　曾任湖南省委书记的叛徒任卓宣向何键献策：“杨开慧如能自首，胜过千万人自首。” <br />
<br />
　　于是审讯官对杨开慧说，你只要宣布同毛泽东脱离关系即可自由。杨开慧却一口回绝：“死不足惜，惟愿润之革命早日成功。”11月14日，杨开慧在长沙被杀害。此时在江西指挥红军反“围剿”的毛泽东，得知杨开慧牺牲的消息后寄信给杨家说：“开慧之死，百身莫赎。”<br />
<br />
　　从以上的史料可以看出，杨开慧对毛泽东是既爱又忠。当局表示只要宣布同毛泽东脱离关系即可自由，按说条件不算苛刻，但杨开慧则毅然拒绝，其一片忠贞可对苍天。<br />
<br />
　　然而又有史料透露，就在杨开慧对敌人坚强不屈对毛泽东忠贞不二之时，她的夫婿已然于1928年在井冈山与新欢贺子珍喜结良缘同床共枕了。由此看来并不是“我失骄杨”，而是“我弃骄杨”。贪恋美色的毛泽东彻底背叛了忠贞的杨开慧，他所说的“开慧之死，百身莫赎”完全是虚情假义。<br />
<br />
　　《毛：不为人知的故事》的作者张戎指出：毛喜欢女人根本不是秘密，从杨开慧起就是如此。我们（在采访中）一个重要发现，就是杨开慧的遗稿。她死前将其诗、信、笔记藏入砖缝与屋檐下，各一包。九十年代维修房子（遗稿）被发掘出来，内容在大陆是绝对保密的，毛亲属也不准看。从那些文件可以看到毛虽然爱杨，但已有很多女友，最后遗弃杨开慧，杨（在遗稿中）表示了对毛遗弃她和三个儿子的怨恨和对共产主义的失望。<br />
<br />
　　如果杨开慧的的在天之灵看到了这首《蝶恋花》，不知会作何感想？<br />
<br />
　　当然，贺子珍最后的结局也如同弃女怨妇。已割据一方的毛泽东在延安得遇上海来的电影明星江青，两眼放光如同见到了天仙，欣然纳之颠鸾倒凤，早把可怜的黄脸婆抛到九霄云外去了，真真是“窑洞只见新人笑，不闻旧人日夜啼”！为了遮人耳目和避免麻烦，毛还强行把身心交瘁的贺子珍遣送到苏联的精神病院，即使解放后也不能居住在北京。<br />
<br />
　　毛泽东对与他同床共枕生儿育女的妻子尚且如此无情无义，对那些战友同志落井下石也就完全可以理解，对中国老百姓视若蝼蚁更是不足为奇了。<br />
<br />
　　因此，依实情试为老毛改之：　　　<br />
<br />
　　我弃骄杨君失柳，割踞井冈又上重霄九。<br />
<br />
　　笑问子珍何所有，子珍捧出黄g酒。 <br />
<br />
　　寂寞江青舒广袖，窑洞欢歌且为孤家舞。<br />
<br />
　　忽报岸英死于虎，泪飞顿作倾盆雨。 <br />
