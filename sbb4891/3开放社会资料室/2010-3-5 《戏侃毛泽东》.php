 <br />
 <br />
　　出处：《毛泽东与贺子珍》<br />
<br />
　　作者：文夫 张乃胜<br />
<br />
　　塘边村遇险，贺子珍与毛泽东的感情迅速升华。毛泽东温存地对贺子珍说：“你是个好同志，好姑娘，我很喜欢你。”他们俩终于结合在一起了。没有举行什么仪式，只是热心的袁文才做了几个好菜，大家热闹一下，就算喝了喜酒。 　　<br />
<br />
　　红四军第一次打下永新后，没过多久，国民党的军队就卷土重来了。 　　<br />
<br />
　　蒋介石听到红军占领永新的消息后，大为震怒，亲自给江西、湖南两省下达了“加紧剿匪”的命令。国民党江西省政府主席朱培德，急急忙忙调集杨如轩的四个团，攻打永新。 　　<br />
<br />
　　毛泽东采取敌进我退的战略，率领红四军主动撤出永新城。然后在草市山的一个山口――草市坳设下埋伏，围歼了敌人，又一次打下永新。这就是历史上有名的二打永新。 　　<br />
<br />
　　1937年，毛泽东与贺子珍在延安 <br />
<br />
　　1928年6月草市坳战斗旧址。红军的胜利，使蒋介石慌了手脚。他再次命令湘赣两省调集更多的兵力，联合攻打永新。 　　<br />
<br />
　　1928年6月，十几个团的敌军，又开始发动了对永新的进攻。他们的指挥是两只“羊”(杨如轩、杨池生)，这两个敌军头目在上两次攻打永新的战斗中，都曾经是红四军手下败将。这次他们仗着人多，武器好，步步进逼。红军士气高昂，以一当十，在新老七溪岭的龙源口打了一仗，大获全胜，第三次打下永新。 　　<br />
<br />
　　红军这一仗，粉碎了湘赣两省敌军的联合进攻，保卫了井冈山革命根据地，开创了湘赣边界根据地的全盛时期。 　　图为行军中的红军。在第二、第三次攻打永新的战斗中，贺子珍仍然负责永新县城附近村庄的地下工作，了解敌情，并同农民武装暴动队的总指挥部一起，发动群众，配合红军行动。 　　<br />
<br />
　　在这两次战斗中，永新的地方武装发挥了很大的作用。特别是第三次，永新全县群众出动了几万人，他们当中的一部分人，连梭镖都没有，就拿着菜刀、木棒，同红军一起追击敌人。他们从各个山头，像潮水一般扑向敌人，织成了一张天罗地网，配合红军，歼灭了敌人的三个团，把敌师长杨池生的胳膊打伤了。要不是这个家伙骑着马，死抱住马头不放，他就溜不掉了。在战斗中，永新有一支赤卫队追击敌人，一直追进永新城，成为这次战斗中第一支入城的队伍。 　　红军第三次攻下永新后，按照毛泽东“大力经营永新”的指示，集中力量争取在一个月的时间内，全力发动群众。把永新根据地建立起来，把这个地区的土地革命搞好。 　　<br />
<br />
　　因此，部队实行分兵，把红军分别组织成一支支工作队，深入永新的各区各乡。工作队的口号是：发动群众，巩固和扩大革命根据地。工作队的任务是：训练建立工农暴动队，筹粮筹款，进行土地革命，建立党的组织和政权组织。 　　贺子珍按照党组织的指示，带着一支工作队，来到西乡塘边村工作。 　<br />
<br />
　　塘边是永新一个比较大的村庄，由大屋、塘边等四个自然村组成，有一百多户人家，五百多口人。在一打永新后，贺子珍曾陪同毛泽东来到这里，进行土地分配的调查，开展打土豪的活动。 　　<br />
<br />
　　以后，毛泽东又派人到这里发展党员，建立党的组织，成立暴动队，组织人民自己的政府。在第二次打下永新后，这里开始了土地革命。塘边分田地的工作，在永新县进行得最早。 　　<br />
<br />
　　贺子珍这次到塘边村，还像第一次来时一样，住进一个贫农老婆婆的家里。 　　<br />
<br />
　　这个老婆婆住的原来是村里一个恶霸的房子，第一次打下永新后，镇压了这个恶霸，把他的住房分给了老婆婆。房子一共有四间，左边是一间，右边是连着三间，右边的头一间就做了客堂。贺子珍来后，就住在左边的一间里。老婆婆有个儿子，参了军，家里还有一个媳妇，一个孙子。他们就住在右边的厢房里。 　　<br />
<br />
　　每天清早，贺子珍便同工作队的同志分头去找贫农协会和村苏维埃的干部了解情况，开展工作。工作队里的军事干部除了作政治工作和群众工作外，还要帮助暴动队员上军事课，进行操练。晚上，老l们干完活回了家，贺子珍又和工作队的同志们分头到群众家里，同老l谈心拉家常，了解情况。 　　<br />
<br />
　　毛泽东和根据地的红军战士在一起。 　　<br />
<br />
　　不久，毛泽东带领一部分战士也到了塘边村。工作队安排他住到老婆婆的家里。老婆婆高兴地把堂屋腾出来让毛泽东居住。 　　<br />
<br />
　　当时，革命根据地刚建立不久，如何分配土地，没有一个统一的方法。各区各乡的做法都不一样，群众议论纷纷。毛泽东已经跑了永新的几个乡，听到了一些意见。他这次到塘边来，就是想进一步调查研究，比较一下几种分配方法的利弊，以便总结出一些经验，制订一个统一的土地法。 　　毛泽东一住下来，立刻听取贺子珍和工作队的同志们的汇报。 　　<br />
<br />
　　毛泽东一面认真地听，一面不停地在小本子上记着，并不时地提出问题，比如：对这种土地分配方案，满意的人多，还是不满意的人多? 满意的都是什么人，不满意的又是什么人?他们的阶级成分和政治态度如何? 　　<br />
<br />
　　贺子珍对这些问题，一时还归纳不起来，她只能如实地反映：赤卫队长是什么意见，妇女主任是什么态度，贫协主席是怎么说的。 　　<br />
<br />
　　看着自己没能够很好地完成党交给的任务，贺子珍感到羞愧，脸涨得通红，手心冒出汗来。 　　<br />
<br />
　　毛泽东丝毫没有责备之意，他和颜悦色地对贺子珍和工作队的同志们说： 　　<br />
<br />
　　“调查研究也是一种学问，学一学也就会了。” 　　<br />
<br />
　　于是，他结合自己多年来的体会，风趣地谈起应该如何搞调查研究来。首先是调查者自己要心里明确这次调查的目的是什么，调查要紧紧围绕这个目的进行，否则，费了很大劲收集到的是一堆无用的材料。另外，要特别注意调查研究的方式、方法，对不同的调查对象，要用不同的方法。对有的人，比如说对土改的积极分子，可以开门见山，把问题直截了当提出来；要是向富裕中农作调查，就要转两个弯，因为他们的肚肠也有几个弯。 　　<br />
<br />
　　贺子珍睁大眼睛听着。她从没有想到过调查研究还有这么多的讲究。一种发自内心的敬佩之情，不禁油然而生。接着，毛泽东提出开几个调查会，请工作队帮助组织、安排一下。贺子珍自告奋勇地承担了这个任务。她心里暗暗地想：“这可是个学习的好机会呀!” 　　<br />
<br />
　　汇报会之后，贺子珍一面帮助毛泽东组织安排调查会，一面悄悄地留心观察毛泽东的工作方法。 　　<br />
<br />
　　她发现，毛泽东每天晚上总要一个人在油灯下工作到深夜，把当天的情况汇总起来，写出第二天需要的调查提纲，做好充分准备。调查会开起来，他亲自主持，亲自记录，把每个人谈话的要点都记录下来。 　　<br />
<br />
　　贺子珍还发现，毛泽东虽然准备了调查提纲，却没有完全按照提纲提问题，而只围绕这些问题，根据不同的调查对象，从不同的角度提问题。贺子珍觉得，每次参加毛泽东主持的调查会，都有不同的收获。 　　<br />
<br />
　　毛泽东在塘边村作深入调查的同时，还到附近的村庄去了解情况。他了解的内容，有农村土地分配情况，也有工商业分布、生产、销售的情况。以后，毛泽东根据自己掌握的资料，写了一个报告，也就是后来他为之十分惋惜的那份被丢失了的《永新调查》。 　　<br />
<br />
　　《永新调查》，贺子珍曾经看过。几十年后，她还记忆犹新地对笔者说：“里面主要谈了经济方面的情况和问题，有关于土地革命方面的情况，也有对工商业政策的意见。当时，有的人实行过左的政策，对工商业者的财产，采取没收的政策。毛泽东实地作了调查后，纠正了这个错误。《永新调查》是毛泽东在宁冈写的，写好以后曾经油印出来，发到井冈山各个团。” 　　<br />
<br />
　　贺子珍同毛泽东一起工作的时间越久，对毛泽东的印象也越深。 　　<br />
<br />
　　这时，在塘边，发生了一件意外的事情。 　　<br />
<br />
　　有一天，一支外县的地主保安队突然向塘边村袭来。四五十名全副武装的敌人，以密集的枪弹向守在要道上的塘边暴动队发起进攻。 　　<br />
<br />
　　塘边地处在遂川、莲花和永新三县的交界。虽然永新的大部分地区已经解放，但塘边附近几个县仍是敌人的天下。从塘边逃出去的地主向附近的保安队报告说，共产党有个中央委员毛泽东在这里活动，蒋介石正以五万大洋作悬赏。“捉拿毛泽东!”保安队这些家伙贪财心切，立即纠集起来向塘边发起突然袭击。正在堂屋同毛泽东分析调查材料的贺子珍，听到枪声和暴动队员的报告，不禁大吃一惊。她知道，塘边附近有一个连的红军，还有毛泽东的一个警卫班。但是，因为事前没有料到会发生意外，队伍全部分散到各个庄子去做群众工作去了，一时无法集中。暴动队员大部分没有枪，只有梭镖，光靠他们能不能顶住保安队的火力，实在是个问题，万一顶不住，毛泽东的安全就没有保障了。 　　<br />
<br />
　　要是毛泽东出了什么事……贺子珍越想越紧张，脑子全乱了。 　　<br />
<br />
　　她抬眼看看毛泽东，他却非常沉着冷静，仍然慢条斯理地抽他的烟，稳稳地坐在那里，似乎什么事情都没有发生一样。事实上，毛泽东正在分析眼前的局势。贺子珍想到的，他都想到了。他觉得，来者是敌人的先头部队还是一小股保安队，尚未搞清楚，这一仗如果冒险去打，有可能陷入被动，危及塘边人民的安全。 　　<br />
<br />
　　于是他果断地作出决定：“通知村里的群众，马上撤出村子!” 　　<br />
<br />
　　塘边村地处在群山之中，走出村落就是山，村里的干部和群众很快都撤到山上。毛泽东同贺子珍，还有老婆婆一家，一起上了山。 　　<br />
<br />
　　他们隐蔽在密林深处，注视着敌人的动向。敌人进村后，找不到人，知道这里的百姓已经有了准备。他们人数不多，怕中了红军的埋伏，不敢久留，在村子里抢掠一阵子就走掉了。 　　<br />
<br />
　　这次遇险，虽然不算什么大的事情，但在贺子珍的生活中，却是一次关键性的、有决定意义的事件。 　　<br />
<br />
　　她亲眼目睹毛泽东在危急的时刻，竟是如此的镇定自若，沉着应付。而这种冷静、自信和力量，正是她所缺少的。 　　在同毛泽东的接触中，贺子珍常常拿自己同毛泽东相比，每次相比，都感到自己年轻幼稚，同时感到在毛泽东的身上，不仅有丰富的斗争经验，而且有无穷的力量。 　　<br />
<br />
　　毛泽东这种优异的、革命者的素质，是这样强烈地吸引着她。每当她来到他的身边，就感到增添了勇气和力量；有他的帮助和指点，自己也变得聪明和成熟多了。毛泽东对她的帮助是十分热心和真诚的。大到政策、思想、方针，小到工作作风、工作方法，都耐心地作指导。 　　<br />
<br />
　　有一次，贺予珍出去做群众工作，到了吃饭的时间，群众要留她吃饭。她想起红军的纪律，婉言谢绝了，饿着肚子把事情办完，回到军部才吃饭。 　　<br />
<br />
　　毛泽东知道后，夸奖她说：“你做得对，老百姓生活很苦，他们留你吃饭，肯定要给你做点好的，这样就增加他们的负担了。” 　　<br />
<br />
　　自从毛泽东上了井冈山，贺子珍就把他当作自己的好老师了，遇到有什么事情想不通，或者不知道应该怎样处理，总喜欢去征求他的意见，请他帮助出出主意。 　　<br />
<br />
　　在反动民团袭击塘边之前，贺子珍对毛泽东更多的是同志的敬爱之情，但是在塘边遇到这次意外事件，他们一起商量对付敌人的办法，一起上山隐蔽以后，她的感情突然起了微妙的变化，产生了一个念头：“要是能够永远在他的身边工作，经常得到他的帮助和指点，该有多好!” 　　<br />
<br />
　　年轻姑娘的心事是从来不肯轻易吐露的。贺子珍虽说是反封建的先锋战士，但一想到自己悄悄爱上一个人，就感到那样不好意思，胸口怦怦直跳，平时的勇敢、大胆、泼辣的作风也变得无影无踪了。 　　<br />
<br />
　　毛泽东从贺子珍那害羞的神态和表情中，看到了这个年轻、质朴的姑娘那颗火热的心。 　　<br />
<br />
　　有一天，贺子珍外出工作回来，和往常一样轻手轻脚地走进毛泽东的房间。她见毛泽东正在伏案写着什么，便一声不响地倚在门框上，深情地注视着。 　　<br />
<br />
　　也不知过了多久，毛泽东停笔沉思，一抬头，正遇上贺子珍那双炽热的眼睛。两个人的目光撞击在一起，撞出了爱情的火花。贺子珍赶紧把害羞的目光移到了自己的脚尖上，不自然地揉搓着土布上衣的衣角。 毛泽东搬过一把竹椅，让贺子珍坐下，沉默了一会儿，温存地对贺子珍说：“你是个好同志，好姑娘，我很喜欢你。”接着，他动情地讲述了他的经历和身世。 　　<br />
<br />
　　毛泽东的第一个妻子杨开慧与他们的儿子岸英、岸青于1924年在长沙合影。贺子珍从毛泽东的谈话中，知道了他追求革命、献身革命的经过。他已经34岁了，结过婚，妻子和三个孩子都留在湖南老家了。 　　<br />
<br />
　　讲到这里，毛泽东神色黯然地说，我同家人已经久不通信了，关山远隔，杳无音信，路途又这么遥远，也不知道他们现在情况如何，是死是活也搞不清。湖南的反动派抓到共产党就杀，听到妻子杨开慧的多种传言，有的说她被国民党抓去了，也有的说她已经不在人世了，不知是真是假。 　　<br />
<br />
　　听到毛泽东这些坦诚之言，贺子珍被深深地感动了。毛泽东为了革命，抛妻别子，孑然一身。他身上的担子那么重，工作那么忙，自己的生活无暇料理，是多么困难啊! 　　<br />
<br />
　　她看看毛泽东那身好久没洗过的旧军装，在敬佩、同情的目光中，又加上了更多的怜爱之情。 　　<br />
<br />
　　那天，他们倾谈了很久很多，谈得那样投机。两颗赤诚的心相印在一起了。 　　<br />
<br />
　　此后，贺子珍跟着毛泽东继续在塘边进行工作，塘边的山问、田野、农家小屋，处处留下他们的脚印。 　　<br />
<br />
　　稻子已经熟了，田野一片金黄。这是塘边人民分得土地后的第一个收成年，乡亲们喜气洋洋。 　　<br />
<br />
　　在开镰前夕，毛泽东向红军战士发出支援夏收的号令。红军从干部到战士，每天清早四点就下田，一直割到晚上七八点钟。 　　<br />
<br />
　　贺子珍闻毛泽东也参加了割稻子的劳动。她是城里人，从来没有干过农活，毛泽东却是作田的好把式，割起稻子来干净利落，连种田的里手也不由得点头称赞。 　　<br />
<br />
　　毛泽东看到贺子珍不会割，就笑着走过来，教她怎样捉镰，怎样割稻。毛泽东的兴致很高，提议大家进行比赛，看谁割得快。贺子珍初学，明知不是大家的对手，也积极参加了。比赛结果，贺子珍被落在大伙后面。可她并不气馁，坚持把分给自己的地垅割完。 　　<br />
<br />
　　在塘边村工作的那些日子里，贺子珍和毛泽东每天都要抽出一些时间帮助房东老婆婆做家务活。老婆婆的媳妇要下田劳动，带孙子、做饭的事把老婆婆忙得不可开交。 　　<br />
<br />
　　毛泽东每天早上起来，总要为老婆婆挑满缸里的水，然后帮她打扫院子。贺子珍便帮助老婆婆抱孙子，或去烧火做饭。他们相处得像一家人。 　　<br />
<br />
　　直到今天，塘边村的老人们忘不了当年毛委员同贺子珍在这里从事革命活动的情景。他们会指着泉水窝上一口清澈照人的泉水井告诉你：这是毛委员看到这里杂草丛生，泉水浑浊，便带着红军战士挖成这口井，让乡亲们喝到清甜的泉水。当年挖井的时候，贺子珍也参加了劳动。他们还会指点着村后那条石基路，对你说，毛委员住在塘边的时候，看到这里没有通路，每天耕牛回村，常到庄稼地里乱踩，于是建议军民一起动手，修了这条路。修路的时候，贺子珍满头大汗地挑过石头…… 　　<br />
<br />
　　就这样，相互接触，共同的情感，贺子珍和毛泽东终于结合在一起了。没有举行什么仪式，更没有摆酒祝贺，在那样艰苦的年月，只能一切从简。 　　<br />
<br />
　　热心的袁文才做了几个好菜，大家热闹一下，就算喝了喜酒。 <br />
<br />
　　老来偶兴补考:　　 <br />
<br />
　　毛泽东温存地对江 mm说：“你是个好同志，好姑娘，我很喜欢你。”他们俩终于结合在一起了。没有举行什么仪式，只是热心的康生做了几个好菜，大家热闹一下，就算喝了喜酒。 　　<br />
