													<h2>译文：彭博社：书评 《毛泽东制造的大饥荒》――人相食，啃泥土，性交易只为了吃的</h2>
						
						
																											<div id="postmessage_815512" class="t_msgfont">译文：彭博社：书评 《毛泽东制造的大饥荒》<br />
――人相食，啃泥土，性交易只为了吃的<br />
<br />
作者：George Walden<br />
发表时间：2010年8月30日 AM GMT+0800 周日<br />
译者：Fuge<br />
校对：@xiaomi2020 <br />
<br />
1976年，当朱丽叶・尼克松・艾森豪威尔1在北京会见毛泽东的时候，她佩戴了一枚毛像章――而且觉得这很有趣。最近，坎特伯雷大主教还哀叹毛的去世带来的损失，他说毛的统治“保证了每个人的福利。”<br />
<br />
在弗兰克・迪克特（Frank Dikotter）讲述毛政权如何导致至少4,500万人死亡的书中――编年体《毛泽东制造的大饥荒》发表之后，没有人再能为时髦的“毛主义”找到任何借口。在书中，他说这场人为的饥荒史无前例、世所未见。<br />
<br />
迪克特是从伦敦大学转到香港大学的教授。他戳破了围绕着毛的谎言和迷雾。从1958年到1962年，毛疯狂地想通过“大跃进”来实现共产主义，超越苏联，获得社会主义阵营的领导地位。迪克特的消息来源是他发掘出来的中央和地方的记录，都言之有据，而成果就是这本让人震惊的书。<br />
<br />
毛的目标是“用两条腿走路”――大力提高农业生产，同时进行工业现代化。在农村，这意味着结束私有制，强制推行集体农庄，这只能让农民们吃掉他们能找到的一切――家畜，庄稼，种子――而不是把它们交公。在城镇，这种政策的结果是进口大量生锈的或者坏掉的机械，让忍饥挨饿、昏昏沉沉的工人们长时间地工作。<br />
<br />
<br />
毛式恐怖<br />
<br />
迪克特说，毛氏恐怖的效率是如此之高，以至于几乎没有留存下来饥荒时的照片 。不过事实已经足够，你从未读到过如此惨痛的内容。不人道，尤其是意识形态下的人们体现出的不人道，灼烧着书中的每一页。<br />
<br />
水肿和疾病到处流行。一些人因为从田地里偷庄稼而被党的官员毒打致死。官员们为自己囤积粮食，也用粮食来做性交易。道路两旁排列着奄奄一息的未穿衣服女人和小孩；他们最后做的事是试图卖掉衣服来换一口吃的。<br />
<br />
孩子被卖掉了。人吃人的现象也出现了。坟地被挖开，尸体被用做肥料。人们吃树叶、树根、有毒的浆果、皮革和偷来的尚未成熟的庄稼，在临死之前，他们甚至吃泥土。<br />
<br />
为了得到建筑材料，房子被拆毁，屋主们被赶进公社――或者留在野外。农具也被熔化，人们建起“小高炉”以达成炼钢的指标。当然，这样炼出来的钢铁都是废品。<br />
<br />
人为的饥荒<br />
<br />
毛政权和它的西方支持者们否认当时发生的事情：他们说，是洪水影响了收成。但是迪克特展示，这种刻意炮制的难以自圆其说的理由是想证明，大自然破坏了排水和灌溉系统。到最后，甚至共产党自己都不得不承认，“三年自然灾害”中有七分是人祸。但是迪克特仍不同意：大饥荒有超过95%是人祸。<br />
<br />
去除了伪装的赤裸裸的谋利动机导致了裙带关系、人与人的恶意交往和从上至下的腐败。每个人都在卖出仅有的东西只求苟延残喘，为了博得毛的欢心，人们甚至把产量数据夸大到了滑稽可笑的程度。<br />
<br />
一些勇敢的人，像国防部长彭德怀和中共中央副主席刘少奇（他在后来的“文革”中被清洗），提出过反对意见，但是毛是皇帝，他的话就是法律。相反，当时的总理周恩来（直到今天，周仍在西方很受欢迎）的行为却很懦弱和自私。为了卑躬屈膝地向他的主子示好，他执行了导致灾难的政策。<br />
<br />
转折点<br />
<br />
毛的大饥荒并不是过时的历史；它是现代世界的一个转折点。“文革”是毛对破坏他的计划的“右派”同事的报复，而从1966年到1976年的“文革”是第二个毛氏灾难，最终，这个灾难在毛死后推动着中国迈向资本主义和更大的自由。<br />
<br />
今日中国的源头和它带来的新的世界秩序都是基于当代最血腥的暴君之一（如果不是之最的话）的残酷无情上。当中国人能够读到这本书的时候，迪克特对历史和这群人作出了自己的贡献。<br />
<br />
谁知道还会有谁呢？也许甚至坎特伯雷大主教也会读这本书。<br />
<br />
<br />
