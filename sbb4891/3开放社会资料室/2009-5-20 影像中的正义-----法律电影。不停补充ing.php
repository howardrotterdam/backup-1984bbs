													<h2>影像中的正义-----法律电影。不停补充ing</h2>
						
						
																											<div id="postmessage_97390" class="t_msgfont">《12 Angry Men》上映年份：1957。这部电影的伟大让我不忍心将其与下面的电影一同排序。仅此而已.....<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;详见豆瓣及其评分：<a href="http://www.douban.com/subject/1293182/" target="_blank">http://www.douban.com/subject/1293182/</a><br />
<br />
<br />
　　 1、《死囚168小时》 Dead Man Walking&nbsp;&nbsp;(1995) ：苏珊・萨兰登奥斯卡奖作品。西恩・潘演一个死囚，死前获得了宁静。而凶杀事实是渐渐呈现出来的，也并非重要，重要的是死囚的心理变化。<br />
　　<br />
　　2、《刺杀肯尼迪 》JFK (1991)：检察官调查肯尼迪案。奥利弗・斯通导演，美国有人说他在不负责任地胡说八道，而另有一个我国网友曾说那个凯文・科斯特纳最后陈词的长镜头让他激动流泪。很好的法庭演讲啊，被很多人赞为经典。<br />
　　<br />
　　3、《伸张正义》 And Justice for All&nbsp;&nbsp;(1979) ：又名义勇急先锋，阿尔・帕西诺主演。北大一老师作文举例称此片主角是一个三流律师。是三流吗？不是，只不过确实是一个在生活中挣扎的律师，为公正与恶法官作鱼死网破式的斗争。说他是三流，太市井味了。本片对律师在公正与职业义务之间的挣扎也有很好的表现。<br />
 <br />
　　4、《甘地传》 Gandhi&nbsp;&nbsp;(1982) ：甘地学法律的，也做律师。本片获得当年奥斯卡最佳影片。<br />
　　<br />
　　5、《丑闻》 (1950)：日本法律电影，黑泽明重要作品。“故事以一个无能而又品质恶劣的辩护律师和一位委托他对损坏名誉案件做辩护的年轻画家为中心展开”，“压轴一场法庭戏差不多长达15分钟，镜头沉实，剧力迫人”。<br />
 <br />
　　6、《民事诉讼》 A Civil Action&nbsp;&nbsp;(1998) ：又名“禁止的真相”“公民行动”，获得当年奥斯卡提名，著名律师电影，约翰・特拉沃尔塔主演，是由畅销作家强纳森哈尔的名作《民事诉讼》改编，为一真实故事。本片也常为国内法学老师提起，比如其中的睡醒后第一句是“我反对”情节。一部对美国现实法律的有很集中很细腻表现的优秀电影。<br />
《民事诉讼》故事叙述一当红律师，因接手一水污染案件而失去了一切；但与此同时，他才有机会认清法律真义及生命价值。他在一无所有的窘境下，继续研究该案，上诉时间长达八年，最后终于胜诉。本故事的构想始于十年前。着名作家哈尔受邀见证许立建律师承办的这件伍本案。许立建期待哈尔能将他叱吒法院蠃得胜诉的精彩过程写成畅销书。当时他是炙手可热、身价不凡的律师；手上已握有足够证据，非常笃定能打蠃官司。然而情况愈来愈糟，他追寻真相的同时，金钱、友谊皆被耗尽，不但一文不名，并负债累累。“我眼睁地睁看见一个人濒临崩溃。”作者哈尔回忆许立建本人说：“那是个无底洞，所有接近它的人都必须接受它的考验，它考验你的认知、信念、选择，迫使你检验自我的价值，真理的必要性，这是此案最迷人之处。”<br />
<br />
 <br />
　　7、《审判》 Procès, Le (1962)：法国电影，奥逊・威尔斯导演。警察询问嫌疑犯的过程。本片好象有美国翻拍版。<br />
 <br />
　　8、《芝加哥》 Chicago&nbsp;&nbsp;(2002) ：歌舞片，获得当年奥斯卡最佳影片。里查・基尔演一个擅长于操作的律师。<br />
 <br />
&nbsp; &nbsp;&nbsp; &nbsp;9、《死囚之舞》 Monster's Ball (2001)：哈里・贝瑞获得当年奥斯卡影后。死囚妻子与狱警的故事，算是法律与心理电影。and,作为一个男人，不得不提到其中贝瑞非常性感的演出。<br />
 <br />
&nbsp; &nbsp;&nbsp;&nbsp;10、《兄弟的监护人》 Brother's Keeper (1992)：美国纪录片。法理争论。<br />
 <br />
&nbsp; &nbsp;&nbsp;&nbsp;11、《惊爆内幕》 The Insider&nbsp;&nbsp;(1999) ：烟草诉讼之证人。阿尔・帕西诺、拉塞尔・克罗主演。<br />
 <br />
&nbsp; &nbsp;&nbsp;&nbsp;12、《永不妥协》 Erin Brockovich (2000)：茱莉亚・罗伯茨凭此片获得当年奥斯卡影后。法律工作者（无牌律师）的故事，中年妇女文化不高，只本着公平信念，在律师的配合下，打下一个巨大的官司。真人真事。<br />
 <br />
　　13、《立体谎言》 City Hall&nbsp;&nbsp;(1996) ：阿尔・帕西诺和约翰・库萨克主演。市长助理调查一起法官受贿案。在感情与仕途之前，选择说真话。<br />
 <br />
　　14、《大审判 》 The Verdict&nbsp;&nbsp;(1982) ：保罗・纽曼演一个穷困于酒的律师办一个重振雄风的案子。本片能反映出在美国“小律师”与“大律师”法庭如何搏弈，富有信息，很能给人各种启发。好片啊。<br />
 <br />
　　15、《杀戒》：年青律师与一个年青凶手的故事，主要可能是探讨死刑价值的问题（时间长记不清）。东欧电影，也能当成一部心理文艺片。<br />
 <br />
　　16、《丑闻》：政治犯实录：韩国电影，并非那古装片。政治犯故事。<br />
 <br />
　　17、《最后时刻》 Reversible Errors (2004)：电视电影。即将伏法杀人犯竟让辩护律师对司法制度的正义性、人性的贪婪及为了不择手段达到目的所付出的代价产生疑问。检视所有在他人生中无法回头的决定，律师雷夫决心好好照顾住在市郊心理不稳定的妹妹，在人生十字路口上他找回了自己。 <br />
 <br />
　　18、《盲井》：提到了我国民工的劳动保险及人身保障的问题。<br />
 <br />
　　19、《伊芙琳》 Evelyn (2002)：爱尔兰影片，布鲁斯南演一个诉争子女抚养权的工人的电影。很好的法律电影。<br />
 <br />
