<br />
79、litongling补充------《Boston Legal》（2004）：典型的民主党编剧写出的本子，并非那种讲推理讲悬念的。千奇百怪的案子，极端精彩的closing，很多都是从法理上来条分缕析。其中不少精彩的对白一定合小组成员的胃口，比如下面这句（出现在第三季某集的最后，偶只记得大意）-------“有时候批评是爱国的最好表现，这是我们这个国家自由的基础。”<br />
<br />
<br />
80、8英里补充------我的表兄维尼（1992）：主要讲的两个年轻人误被当作杀人犯抓起来，所有证据都对他们不利，JOE PESCI演的一个乡巴佬律师在法庭上完全不能控制局面，就在两个年轻人一步一步滑向深渊时，律师和她老婆突然来了个大逆转，将两个年轻人拯救了。看了此片后，让我觉得在美国人人都能当律师。多美好的社会啊。<br />
<br />
81、george补充------《性书大亨》（1997）：赖瑞-佛林特曾经是脱衣舞俱乐部的老板，后来成了色情杂志“好色客”的发行人。因为“好色客”走低级、煽情路线，比起“花花公子”杂志有过之而无不及，所以引来卫道人士的抨击，也使得有关当局找上门来，而其中的某次冲突，使得他的官司一路打到最高法院，变成一宗历史性的知名案件。而原本声名狼藉的赖瑞-佛林特，在长期与法律制度抗争的过程中，无形中却使得这场事关情色的言论风暴，演变成有关美国宪法保障言论自由、公民自由的象徵。<br />
<br />
82、lm70cc补充-------《正当防卫》（1997）：伊维格市发生了一件女童凶杀案，声名显赫的法律系教授阿姆斯壮，接到死刑犯巴比的请求信，声称当年是屈打成招认罪，阿姆斯壮来到小镇重审当年轰动一时的谋杀案，循线追查下真凶呼之欲出，剧情急转直下。<br />
<br />
83、lm70cc补充-------《恐怖角》（1991）：因强奸罪入狱十四年的卡迪刑满释放了。十四年前，律师萨姆出于道义隐藏了对卡迪有利的一份证据，致使卡迪被施以重刑。经过狱中十四年的磨练后，卡迪来到萨姆居住的小城，开始了他疯狂的复仇行动。 卡迪首先出现在萨姆一家的周围，造成他们心灵上的恐慌。而后他诱引并殴打了萨姆的情妇，又开始接近萨姆的女儿丹妮。恐惧的萨姆雇佣了打手袭击卡迪，却被凶猛的卡迪打得落花流水。一天深夜，卡迪潜入萨姆的住所……《恐怖角》似乎在讲述一个复仇天使的故事，细究这部电影，觉得非常有意思，你难免要问自己：什么是公平公正？什么才是正义？<br />
