																											<div id="postmessage_811595" class="t_msgfont">学界苦行僧茅海建<br />
[日期：2010-03-11] 来源：爱思想&nbsp;&nbsp;作者：张鸣&nbsp;&nbsp;[字体：大 中 小]&nbsp;&nbsp;<br />
<br />
&nbsp;&nbsp;<br />
 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp; 近代史学界，看档案看得最多的人是谁？如果要投票的话，估计半数以上的学者会投给茅海建。中国第一历史档案馆几乎就是茅海建第二个家，只要他没出国，也没有课，要想找他，直接去中国第一历史档案馆就是，他肯定在那儿。如果人家大年三十不放假，他可能也会去。即使在家过年，他的大半时间也是在啃那些从馆里复制来的档案。做他的学生，是个苦差事，他未必要求你怎样怎样，但总是如此带头吃苦，让学生想要偷懒都不好意思。 ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> ) <br />
&nbsp; &nbsp; 茅海建不会打牌，也不喜欢下棋，好像也没有什么打球爬山之类的爱好。还好，偶尔跟朋友小酌一下，还是可以的，我就是这样认识他的。在酒席间时，大概是茅海建唯一非学术化的时刻，可以开玩笑，互相打趣，甚至小规模地谈谈风月，他也不会强烈反对。但是，如果席上有谈锋锐利的罗志田的话，茅海建就会落得只有被打趣的份儿，只能偶尔反击一下，一点效果都没有。 ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> ) <br />
&nbsp; &nbsp; 近代史界，过去人称南桑北茅西南罗，分别是桑兵、茅海建和罗志田，现在这样的学术地图也许要变了，但三位雄霸的局面却没有多大变化。三者之中，我跟茅海建最熟，十几年前就认识了。当时他刚刚写出《天朝的崩溃》，海内震动，也不知道出版社印了多少版，就当时而言，是少数几次学界风暴波及社会的事件之一。据说，不仅某些学界元老大感不快，连有关单位也表示关注，众口一词，说他为汉奸翻案。其实呢，这本研究鸦片战争的著作，无非讲了几句大实话，而且句句都有档案的根据。 ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> ) <br />
&nbsp; &nbsp; 不过，自茅海建之后，学界根据所谓“政治正确”来评判学术著述的风气，居然为之稍杀，后来者日子好过多了。可是茅海建却为此付出了很大的代价，直到前两年，北大评他长江学者，还因为此事而作罢。迄今为止，茅海建是几位史学界大腕中，唯一一个头衔特别简单的人，没有官衔，也没有各种“江学者”之类的荣誉衔头。 ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> ) <br />
&nbsp; &nbsp; 好在，茅海建不在乎这个，提起来，也会发句牢骚，就一句。他真正在乎的，是他所研究的历史的真相。在我的案头，摆着一本大砖头一样的著作：《从甲午到戊戌康有为〈我史〉鉴注》，83万多字，是茅海建整整6年的成果。就像他自己说的，研究戊戌变法，他是个后来者，但一本《戊戌变法史事考》，已经让人刮目相看。没想到人们刮目之后，他居然花了这么长的时间，弄出来这样一块砖头，仅仅是为了给研究者提供方便。 ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> ) <br />
&nbsp; &nbsp; 凡是碰过戊戌变法的人都知道，康有为的《我史》，即康南海自编年谱，是个绕不过去但又没法用的史料。康有为是事件第一号亲历者，这个变法，过去人称康梁变法，他的叙述，自己整理的相关史料，能避开吗？但是，康有为这个历史上空前绝后的变法伟人，又是一个空前绝后的自恋狂，他和弟子们，在后来的岁月中，花了好大的精力，来打造一个完美无瑕的教主，一个新时代的圣人，一个旷世的帝师，结果，这个自编年谱，就真假难辨了。 ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> ) <br />
&nbsp; &nbsp; 茅海建为了能让这本年谱从无用变成有用，居然耗费了自己6年工夫，一条条地考订，有时为了证实一件小事，比如《日本变政考》的进呈，居然查阅前后一段时间全部的清宫档案：军机处《随手档》、《洋务档》、《电寄档》……现在，厚达900多页的砖头问世了，后来的研究者有福了，茅海建，也老了。 ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> )<br />
&nbsp; &nbsp; 必须提一句的是，茅海建那本耗费6年心血的著作，在国家社科课题评审中，居然被评为暂缓结项项目。 <br />
&nbsp; &nbsp; 在当代史家中，茅海建的文笔很好，罗志田和桑兵也是如此，不仅他们自己，连他们带出来的学生，行文都有一股民国学人的味道。罗志田给《南方周末》开的专栏，没点耐心和文化水，看起来估计困难。茅海建的文字也是这样，别的不说，有股醇味，不像现在流行的时文，不是像白开水，就是像白开水里兑了太多的香精和糖精。可惜，他们都不大乐意写面向大众的文字，罗志田好歹还有一个专栏，茅海建则满打满算，只有一本勉强可以算作历史随笔的书：《苦命天子》。 ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> )<br />
&nbsp; &nbsp; 眼下，茅海建已经南行，以后再见他，要去上海了。即使是北大，也一样留不住人。</div>

							
							
							
							
