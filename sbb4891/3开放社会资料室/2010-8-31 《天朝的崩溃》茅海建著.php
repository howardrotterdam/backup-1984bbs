																											<div id="postmessage_811596" class="t_msgfont">石冷：茅海建《天朝的崩溃》读后 <br />
作者：石冷&nbsp; &nbsp; 文章来源：本站原创&nbsp; &nbsp; 点击数：1013&nbsp; &nbsp; 更新时间：2005-10-22 <br />
 <br />
讲真话・民族自省・学术遭遇 <br />
 <br />
――读茅海建《天朝的崩溃》 <br />
 <br />
石　冷 <br />
<br />
　　史学要不要讲真话?这似乎是一个相当荒唐可笑的问题。尽管史家们已认识到要完全弄清历史的本来面目是不可能的，但这不等于人们在历史研究中可以说假话。不过，咱们国情独特，其中之一是公不公私不私，闹得学术界颇不安宁。 <br />
　　学术是公器，理应让学者自由参与，然而，有些人却偏偏化公为私，一言九鼎。查语录、看风向、对口径代替了诚实、刻苦的研究，连绵不断的假大空对人文与社会科学的糟蹋，制造了多少不堪卒读的所谓“学术著作”乃至什么“重大学术成果”，尤其是自苏联移入而与中国古老传统相结合至今仍不时可以看到的怪胎――“革命大批判”。 <br />
　　从70年代末开始的史学研究特别是中国近代史研究的变革，很大程度上是从重新厘定公私界线，重新做好研究的基础工作开始的。这个变革的基本特征是求实。作为一直注视和参与这一过程的一员，我看了新近出版的茅海建著的《天朝的崩溃――鸦片战争再研究》(以下简称《天朝》)一书后，涌上心头的是“轻舟已过万重山”的欢快和感慨。这不仅是对又一杰出史家脱颖而出的欣喜，而且庆幸中国近代史研究终于走上了求真求新的正轨。 <br />
　　200年来，影响中国历史行程的最大事件莫过于鸦片战争。人们所说、所写、所论不知凡几，但关于这次战争的一些最基本的情况却淹没在史实与神话交织的烟雾中。 <br />
　　鸦片战争前清政府内部存在严禁派与弛禁派的斗争吗?促使道光帝下令严禁 鸦片的是林则徐吗?当时的广州有叫做“平英团”的组织吗?琦善是不是卖国贼， 他有没有陷害过林则徐，关天培之死和虎门战败是他的过失吗?吴淞之战失败、 陈化成战死是两江总督、“投降派”牛鉴的罪过吗?战争中清廷内部有没有主战 派与主和派的斗争?是不是坚决依靠林则徐等人，中国就有可能打赢?……如果这 些问题的答案都是“不”，那么，已经被中国人接受的有关观念就会受到全面的 冲击。而《天朝》的作者恰恰毫不留情地向传统的观念挑战，坚定地说了一系列 “不”字。 <br />
　　这是一记当头棒，令人震撼的不仅是它的结论，影响更深远的是它的方法。 作者考虑和研究了十年，对中国和外国的史料一一重新梳理，对现有论著中的结 论一一重新审查；是其所是，非其所非，冷峻严谨，拒绝个人情感的羼入。现代 史学传入并在中国逐步生根已经超过百年，这些方法本来是治学的ABC。不幸，由于错误思潮的根深蒂固，这种实话史学常常招致奇怪的非议乃至沉重的压力，而“以论带史”、以抄代著和有意无意歪曲或割裂史料的恶习，却在史坛流行起 来。于是，一部实实在在的学术著作便成了抗击逆流的堤坝和回到学术正轨的路 标。这个学风转变的影响是极为深远的。 <br />
　　茅海建的成功不仅在于史料的搜集、整理和分析，还由于他的深邃的思想。 如果仅有前者，终究逃不脱匠气，不可能成为卓越的史家。他的史识高超之处在 于一直紧紧抓住一个基点：一贯沿用的坏人当道、好人遭厄的“忠奸模式”不能 解释鸦片战争的必然失败，“正是让‘奸臣们’承担了本应由中国旧体制承担的 责任”(19页)，“鸦片战争的真意义乃是用火与剑的形式，告诉中国人的使命： 中国必须现代化，顺合世界之潮流。”(25页)换句话说，是否有利于改革中世纪的旧体制和推进近代化事业是臧否人物、评论史事的最高准则。这个基本点的确 认不但对鸦片战争而且对整个中国近代史的研究都有非常重要的意义。反对那种 随意涂抹历史的忠奸理论，着眼于当时的制度和文化，这对鸦片战争乃至整个中 国近代史的研究，不愧是知本之言。 <br />
 <br />
　　正因为抓住了根本，《天朝》对皇帝及其文武大臣和山野草民行为方式的剖 析成了全书最具特色的笔墨。20世纪的中国人都痛骂帝国主义在中国享有治外法 权，可是，这是当时大清帝国的统治者主动拱手送给英国侵略者的。悲剧酿成于 文化差异。“在西方，商业活动已不再受官方的具体管制，商人只需遵从法律即 可”；而在中国“每一个人都在官府治理的网络之中，很难想象脱离这种治理之 民。国家设官治民。既然中国官府无法治理这些英人，把他们交由英国官府来治 理也不失为一种办法。”(497页)愚昧的钦差，就是这样顺理成章照会约翰牛，申明如有交涉狱讼之事，“英商归英国自理，内人由内地惩办，俾免衅端”(494 页)。 <br />
　　人们至今仍热衷于歌颂三元里人民的爱国主义。但作者却直率地告诉读者： 　　我以为，三元里民众抗英斗争，无疑是一件值得百年称颂的事件，但将之提 升至民族主义或爱国主义的精神展现，则脱离了当时的时代。 　　在鸦片战争以及后来的诸次列强侵华战争中，绝大多数民众的态度，是置身 事外。(313页) <br />
　　三元里等处民众进行的是一次保卫家园的战斗，而不是投身于一场保卫祖国 的战争(311页)。 <br />
　　人的行为方式不能不受特定社会的文化状况的制约，清帝国的臣民也不例外。 林则徐、琦善、耆英……和所在皆有的顺民和旁观者都是中国传统文化造就出来 的。这读来虽然令人痛心，但与某些文化明星编制的华彩乐章相比，要实在和可 靠得多。文化本来是历史的产物，让文化回到历史特别是近代中国的历史中去， 摈弃无根游谈，也许有助于在思想文化领域中清除泡沫，留下更多结结实实的成 果。 <br />
　　作者过人的史识还在于把民族自省规定为史学的基本任务。<br />
　　我们在历史中看得最少的是，中国人在这个过程中究竟犯了哪些错误，尽管 历史已经明白无误地说明，我们犯过错误。 <br />
　　历史学最基本的价值，就在于提供错误，即失败的教训。所谓“以史为鉴”， 正是面对错误。(25页) 　　一个民族对自己历史的自我批判，正是它避免重蹈历史覆辙的坚实保证。(2 6页) <br />
　　真是掷地有声，发人深省。这正如当代法国年鉴学派巨子雅克・勒高夫说的： “拒不思考历史的民族、社会和个人是不幸的，同历史遗亡症引起的伤害相比， 往昔的创伤微不足道。”(史学理论研究.1994，4.91) <br />
　　近代中国有一个特殊情况：中国是被侵略者，总的说来是正义的一方，而列 强是非正义的侵略者。晚清的官绅和20世纪的一些知识分子，往往以此为理由把 肯定西方和检讨本国弱点或错误的言论视作大逆不道。出现这个思想迷误的原因， 在于忘记了中国除了反对侵略维护自己的主权外，还要学习外来的先进文化和反对本国的中世纪制度及其意识形态。而有识之士肯定的恰恰是西方的先进文化，对本国抨击的内容只不过是那些中世纪专制主义的黑暗和愚昧。历史已经证明，这些人，如一再遭受流俗痛诋的郭嵩寿、胡适、鲁迅等人，其实都是在中国近代 <br />
思想史上留下了光辉足迹的杰出爱国者。 <br />
　　这些有深切民族自省精神的先驱者所以不被理解甚至被诅咒为“汉奸”、“ 买办”的重要原因之一是有些人陷入了狭隘民族主义的泥淖。被侮辱、被损害激 起反抗，这是可以理解的。但无论个人还是民族，成熟的标志是从情绪化走向理 智。这就要求在观察和处理事关国运的大事时要有世界全局的认识和眼光。用先 驱者的话来说，这叫“世界人”意识。 <br />
 <br />
　　1899年，梁启超在第一次奔赴美国的旅途中，写下这么一段话：“余盖完全 无缺不带杂质之乡人也。曾几何时，为十九世纪世界大风潮之势力所簸荡、所冲 击、所驱遣，乃使我不得不为国人焉。浸假将使我不得不为世界人焉。……既生 于此国，义固不可不为国人。既生于此世界，义固不可不为世界人。”(汗漫录. ：清议报，35册)在任公看来，乡人――国人――世界人，乃文化思想升华的 三个阶段，是现时代中国人发展的历史，又是无可逃避的责任。 <br />
　　事隔18年，这个中国人――世界人的问题又一次被提出。说这话的是鲁迅： “许多人所怕的，是‘中国人’这名目要消灭；我所怕的，是中国人要从‘世界 人’中挤出。我以为‘中国人’这名目，决不会消灭；只要人种还在，总是中国 人。……但是得想在现今的世界上，协同生长，挣一地位，即须有相当的进步的 智识，道德，品格，思想，才能够站得住脚：这事极须劳力费心。”(《热风・ 随感录三十六》)什么是“世界人”?也许可以简单地界定为具有世界历史和现状 基本知识和全球胸怀的人。在健全的教育制度下，一个高中毕业生不难达到这 样的水平。一个民族只有多数成员具有这样的素质，才是真正的现代民族。 <br />
　　“世界人”意识的养成实质是整个民族素质的提高过程。整个国家的经济能 否达到和保持世界发展的平均水平以上并融入全球一体化的大潮之中，是决定这 个过程成败的基础。健全的教育和思想文化环境亦是重要的动力，两者互为因果。 生为中国人的困难在于：来到这个世界之日起，就被一套完整的思想文化体系所 笼罩，即以本民族为中心的“天朝意识”。历史已经反复证明，不冲决这个思想 罗网，要如实地认识世界、认识本国只是一句空话，更谈不上正确处理本国与外 国的关系。有位年轻学者说得好：“中国人的现代化过程就是中国人文化心理结 构世界主义化过程。”(曲一日：鲁迅“世界人”概念漫议.现代与传统，第八辑) <br />
　　茅海建的史识归纳到一点，就是有从世界历史发展全局去观察中国命运的眼 光。这种眼历来是近代中国先进知识分子的现代爱国主义赖以滋生的思想基础。 它与中国知识分子的使命感与忧患意识是融为一体的。杰出史家的力作必然是“ 有思想的学术”。大多数读者很可能会和我一样，为《天朝》一书学术与思想交 融的强烈震撼力而激动和沉思。 <br />
　　这部书从社会形态--制度与思想文化角度去剖析历史，与马克思的史学思想 完全一致。更令我震惊的是它与马克思对鸦片战争的直接论述也有那么多的共同 点。马老先生曾毫不含糊地谴责英国侵略中国的不义战争和侵略者的残暴。与此 同时，他也指出 <br />
　　当时人民静观事变，让皇帝的军队去与侵略者作战，而在遭受失败以后，抱 着东方宿命论的态度服从了暴力。(见：波斯和中国・马克思恩格斯选集：2.北 京：人民出版社，1972.19) <br />
　　对于这场战争的总态势，马克思说得更加尖锐：　　半野蛮人维护道德原则，而文明人却以发财的原则来对抗。一个人口几乎占人类三分之一的幅员广大的帝国，不顾时势，仍然安于现状……因此竭力以天朝 <br />
尽善尽美的幻想来欺骗自己，这样一个帝国终于要在这样一场殊死的决斗中死去， 在这场决斗中，陈腐世界的代表是激于道义原则，而最现代的社会的代表却是为 了获得贱买贵卖的特权――这的确是一种悲剧。(鸦片贸易史.同上.26) <br />
　　在他老人家眼中当时的中国人和中国是“半野蛮人”和“陈腐世界”，而侵 略者却是“文明人”和“最现代社会的代表”!这决非一时失言的论断表明，《 天朝》的思路和贯彻始终的民族自省精神，包括对清帝国官方的愚昧、制度缺陷 及文化弊端的揭露都可以在马氏论著中找到端倪。作者没有从语录出发，得出的 结论却与马克思的思想一致。窃以为其中的奥秘在于两人都实事求是地对这件大事进行了认真的研究。 <br />
　　茅海建把他的巨著献给他的导师陈旭麓教授，这是一份十分恰当的厚礼。陈 先生是当代中国最有成就的史家之一，却一再遭受非学术的阻力和压力，他的光 彩照人的653条《浮想录》也只好留待身后公布。陈先生的遗言中有这样的名句： <br />
　　民族主义是动物界的群居本能的升华，“兄弟阋于墙，外御其侮”和“血浓于水”都是本能而不是理性的表现。 <br />
　　中国人的“天朝意识”和“世界意识”的互为消长，是闭关与开放的两重奏。(学术文存.1355～1357) <br />
　　据本书可以看出，茅海建先生不仅深得其师追求真理的勇气与治史的理路，而且有所发展。薪尽火传，陈先生当含笑于九泉，学术界也可额手称庆。</div>

							
							
							
							
