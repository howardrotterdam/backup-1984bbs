																											<div id="postmessage_56389" class="t_msgfont">《寻找家园》读后感<br />
<br />
&nbsp; &nbsp; 这是我读过的第六本右派分子写的回忆录。<br />
&nbsp; &nbsp; 本书作者高尔泰，著名美学家、画家。1957年因发表《论美》，被打成右派，从此20多年，家破人亡，九死一生。但是他从没有停止对专制统治的抗争，直到1992年流亡海外。<br />
&nbsp; &nbsp; 感谢互联网让我读到这么深沉的文字。可以说，这是我读过的右派分子们写的最好的文章（另一个人是朱正，其次是邵燕祥）。这本书其实不是严格的回忆录，作者本人也同意把他算作散文。但是正如有人评价的那样，是“非虚构散文”。它们建立在一个人、一个时代土厚水深的生活基础之上，又继承了汉语文学最美的形式，因此称为当代汉语文学的顶峰当不为过。有人就把它的地位与《红楼梦》相提并论，虽然它的内容和写作手段与红楼完全不同。<br />
&nbsp; &nbsp; 我是从《读书》杂志知道高尔泰的名字的。还是在十多年前，大概看了他两篇文章，不甚了了。想不到他的一生竟蕴藏了那么多故事，一写就30多万字。从少年在抗战风火中的求学，到老来在海外卖画为生。一个真实的人的一生跃然纸上，一个被人遗忘的时代重新出现在读者眼前。<br />
&nbsp; &nbsp; 某网站一位介绍本书的网友说，他把本书作为孩子暑假读物中的一本。但是，因为大陆花城出版社的版本删改较多，他不愿意让孩子“一开始就读这种被阉割了的文字，难免反倒觉得高先生文理不清，只怕从此倒了胃口，更恐有污于高先生清名。”，因此他费劲从网上搜集补充了一些文章，作成了一部较完整的电子书。我就是看的这本。谢谢他。然而当我看了四分之一的时候，我忍不住又找到那个帖子，回帖说：“我感觉，这种书，给孩子看未免太沉重。青少年还是多给他们看光明的东西。过早地看到黑暗，容易产生晦暗的心理。”是啊，我不忍心让孩子们看到这一切。我自己就不看《南京大屠杀》之类的电影。我曾经说过，灾难不是艺术。然而这是历史。但愿这历史只留给老人来感叹。<br />
&nbsp; &nbsp; 我无法把读后感写得更详细。因为，其中的每一篇。每一段都让我感慨不已，甚至热泪纵横。而且，看了书中收录的几篇评介文章，我也没有更多的话唆。所以，只记下一笔，以I不忘。<br />
