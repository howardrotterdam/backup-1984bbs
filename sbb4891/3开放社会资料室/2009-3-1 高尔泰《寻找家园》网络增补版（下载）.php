																											<div id="postmessage_46398" class="t_msgfont">王元化先生<br />
高尔泰<br />
<br />
&nbsp; &nbsp; 一 <br />
　　八十年代初，我甫出深渊，很少朋友。特别是与名流大家，更不蒂霄壤。带著底层的傲慢，孤狼一般游荡。 <br />
　　《论美》出版以后，《读书》杂志的董秀玉大姐建议我寄一本给王元化先生。给了我一个上海他家里的地址。说，“王元化先生，很好的。” <br />
　　回信长达六页，批评极其中肯。指出了许多具体错误，某个概念不明确，某个提法不周延，甚至错字别字，“应属手民误植”。没有应酬性的赞美，但很鼓励我的探索。还问及身世，有一种对命运的关切。我很感动，也很敬佩，从此开始通信。 <br />
　　那时言路乍开，容易出轰动效应。人们习惯于用假套话交往，已经太久。说一句简单的真话，就成了深刻思想。摆一个平凡的事实，就成了重大发现。又碰上美学热，书卖得可以。先生提醒我，忽冷忽热，是不成熟的社会的特征，当不得思想价值的量度。读之深自警省。 <br />
　　先生治古代文论，学贯中西。其文其书，土厚水深。作为那个方面的权威专家，他同时也有一份公民的责任感：关心国事，致力反“左”。笔下有雷声，发聋振聩。（自“反右”以来，“左”、“右”截然两分，概念颠倒模糊。但其引申义已被普遍接受，吾从众。）后又亲自撰文，“为五四精神一辩”，凌厉磅礴。 <br />
　　以为人如其文，也凌厉磅礴。后来接触多了，才知他性格宽和，为人厚道。他曾被打成“胡风分子”二十多年，吃尽苦头。有一次偶然谈起舒芜（因“揭发”“胡风集团”而被众人辱骂）。先生说，人言可畏，舒芜其实是被人利用。个中委屈详情，非当事人不能尽知。舆论对他的惩罚，超过了他所应得，实际上很不公平。 <br />
　　冯友兰先生逝世，我收到宗璞女士一信，说她父亲生前嘱咐，墓碑要我书写。我生也晚，无缘见一代宗师。唯读其书，高山仰止。听说他文革中支持毛、江，文革后成众矢之的。不明就里，打电话问元化先生。先生说乱世做人很难，冯友兰更不容易。设身处地，其情可恕。许多人（说了几个名字）都是那样，现在仍被尊敬。一边厢积诽销骨众口铄金，一边厢开口大师闭口文豪，也很不公平。 <br />
　　公众舆论，往往人云亦云。个人身在其中，须得特别警醒。我恭敬书写了墓碑，和墓碑反面的“三史释今古，六书纪贞元”十字，从此成了宗璞大姐和她的先生蔡仲德教授共同的朋友。时至今日，二位每有新著，必惠赠。文章观海波澜阔，学问游山泉脉多，受益匪浅。 <br />
　　且喜燕南园里，三松依旧龙蟠。 <br />
　　先生对我的教益，诸如此类还多。不止学问，也包括做人。我为人（据朋友们说）心胸狭仄脾气暴躁，言行乖戾不近人情。在先生的帮助下，起码许多事情，处理得比较得体。 <br />
　　先生是国务院学术委员，顶尖名流。也做过宣传部长，周旋官场。如此对待后进，更令人肃然起敬。 <br />
　　他不光是对我如此，对别的青年也是一样。每看到可取的文章，必欣欣然逢人便说。即使作者是边远省份籍籍无名的小人物，也总要找到下落，去信鼓励帮助。陕西师范大学青年教师尤西林，甚至得到一帧他的亲笔书法：“健笔凌云”。 <br />
　　四个字元气淋漓， <br />
　　此四字，后生小子尤西林当之无愧。 <br />
　　除了王元化先生，有谁肯说？ <br />
　　二 <br />
　　第一次见到先生，是一九八七年的事了。 <br />
　　那年我在成都，去了一趟北京。为北师大的文艺美学博士研究生罗刚、刘晓波的毕业论文进行答辩。先生是答辩委员会主任。成员除我和他们的导师童庆炳、张紫晨外，还有北大的谢冕、人大的蒋培坤等。一般来说，委员会五六个人够了，通过论文和授予学位以后，即自动解散。罗刚的答辩就是这样。 <br />
　　但刘晓波离经叛道，不受控制，有关方面想治他一下，又多安排了几个学者进入答辩委员会，使委员会的人数增加了一倍。消息传出去，来旁听的很多，有好几百人。以致不得不把答辩的地点，由会议室搬到了小礼堂。先生的学问人格，受到“左”、“右”两派共同的尊敬，经由他的整合，委员会事先取得了共识。会上气氛和谐，刘晓波顺利过关。 <br />
　　在会上我读完评语，多说了几句话。我说现在不是五四时期，但仍然有一个救亡的问题。那时是救国家，针对外国侵略。现在是救自己。所以现在的文化运动，需要更多的刘晓波。这种能独立思考的人才，越多越好。两年后形势逆转，有人在《文论报》上揭发我说了这几句话，记性可真是好。 <br />
　　那天散会以后，王元化先生约我晚上到他房间里谈谈。他说启蒙问题，不能光讲勇气。关键是启什么蒙，用什么来启。五四成分复杂，也未可一言蔽之。事实上早在1919年之前，中西文化论战、新旧文学论战、问题与主义论战、国故论战、科玄论战等等，都已经有了萌芽。也不光是民主主义和民族主义，那时国家主义，社会主义，无政府主义、基尔特社会主义等等，也有其国际国内背景。他举了几个例子，说明政治文化之脉络交错，都很典型。他说我们回顾以往，可以从工具理性的角度来认同科学与民主，但现在更需要强调的，是自由与人权。 <br />
　　我说，是。 <br />
　　他说比方说多数和少数的关系，国家主权和个人的人格独立之间的关系等等。这些关系不讲清楚，其它的问题都很难讲清楚。现在有些人一讲民主，就说民主是目的，不知道民主只是实现个人自由的手段；有些人一讲自由，就热衷于反逻辑和非理性，不知道自由只能与规范共生，就是因为这里面的关系，没搞清楚。 <br />
　　我说，是。 <br />
　　我说，现代自由主义不同于古典自由主义之处，在于它以个体为本位，而后者以群体为本位。但是承认特殊性和偶然性的价值，承认个体要求的合理性，哪怕是存在主义意义上的合理性，同时也就必须承认，个体利益之间的冲突是不可避免的，它需要某种制约和平衡。需要某种普遍性，哪怕是形而上的普遍性。反逻辑和非理性的思潮，恰恰是以不承认普遍性为前提的。消解了普遍性，也就消解了文明的内在结构，并且把自由问题，由外向的条件开拓，变成了内向的意义追寻。由向强权挑战的政治，变成了向虚无挑战的哲学，这是个新问题。 <br />
　　先生说，是个麻烦。但是寻找普遍性，或者说重建普遍性，弄不好就是本质主义，回归古典，甚至回归宗教文明，更要小心。 <br />
　　我说，是。 <br />
　　三 <br />
　　答辩完毕，我们在北京滞留了几天，各自看望朋友。先生在回上海以前，约我和小雨，还有《人民日报》的王若水夫妇，到西单的豆花饭庄吃饭。说这次来，见了几个老朋友，谁谁谁，都建议他办一个刊物。说谁和谁那里，还有一点儿钱，加起来也够了。建议我们一起来做，办一个以知识分子为对象，以理论研究为主轴的学术性刊物。不媚时阿世，不屈从权力。适度保持低调，以期起一种长远的作用。 <br />
　　那时随着商业大潮的兴起，人文精神正在急剧衰落。知识分子们也相应地愈来愈重学问轻思想，重科技轻人文，有如乾嘉盛时。正需要一种人文努力，来赋予学问以思想，赋予思想以学问，以免二者偕同沦入市场，成为商业的附庸和政治的装饰。先生的想法，正符合时代的需要。虽然很难实现，我们都愿意试试。 <br />
　　刊物叫个什么，颇费思量。“问路人”、“拓荒者”太文学化；“思想家”、“门”端着架子；“文化中国”过于专业；“启蒙”有古典味。我问就叫“大时代”如何，先生说，空泛了些。先生倾向于叫《时与潮》。不久又来信，问改为《新启蒙》如何。 <br />
　　这就是后来闹得沸沸扬扬的所谓“新启蒙事件”的原来。 <br />
&nbsp; &nbsp; 期刊登记困难，先生在上海，想了很多办法，都不行。最后只得以丛书的形式登记，叫《新启蒙》论丛，由湖南教育出版社出版和发行。这中间的曲折艰辛，一言难尽，都是先生一个人承担了。我在成都，若水在北京，一点儿忙都没帮上。 <br />
　　先生独力准备就绪，并试编了创刊号《时代与选择》以后，于1988年十一月，在上海师范大学召开了一次为期三天的编委会，叫“新启蒙笔会”。参加者有邵燕祥、金观涛、于光远、于浩成、李洪林、戈扬、阮铭……等大约二十多人。有人捅给香港报刊，被报道为“国内民主派的大结集”。先生十分生气，但已不能补救。 <br />
　　散会后，先生留我和小雨，还有王若水夫妇在上海多住了几天，商量具体的编务。我们一致同意，刊物要有个性，但须合法。要勇敢真诚，但不硬闯雷池。商定我负责第二期和第三期，王若水负责第四期和第五期。先生转交给我一批稿子，都是他约来的。里面有不少好文章，挑选一下，基本上够两期用了。 <br />
　　编务照常进行。我们都更加小心翼翼。为了免得引起猜疑，先生还谢绝了一切来自海外的赞助。时值1989年春天，我尽量保持低调，避免被潮流带著走。第二期《危机与改革》、第三期《论异化概念》，都总算是出来了。说来惭愧，我只是选了一下稿子。繁重的具体编务，主要还是先生和他的几位才华横溢的博士研究生承担了。 <br />
　　四 <br />
　　后来我不得不离开南京大学。我走投无路，再一次饱尝了人情冷暖，世态炎凉的滋味。先生却不避嫌疑，邀我和小雨到上海他家，住了几天。 <br />
　　先生的夫人张可大姐，是翻译和研究莎士比亚的专家。一场大病以后，一直没完全康复。很少说话，多数时间只是静静地坐着，听先生和我们说。白发如雪，面带微笑，把优雅高华的气息，温馨亲切的感觉，散播到整个客厅。这种感觉，这种气息，给我们留下难忘的记忆。平时很少出门的她，也和先生一起，陪我们参观上海市博物馆，玉佛寺和龙华寺。还时不时指点我们，留心一些值得留心的东西。 <br />
　　龙华寺住持明D法师是全国政协委员，素食招待，并送了我们每人一本他的诗集。素食好吃极了。诗呢，俗情更比僧情浓。我给先生说，没想到兰若精舍，也可以是终南捷径。先生说我少见多怪。 <br />
　　我小时候喜欢十九世纪俄国文学，受十九世纪俄国民主主义的影响很深。也因此，对经常翻译俄国文学及其理论的满涛和贾植芳这两个名字，熟悉而且喜欢。有些翻译家，也成了我的精神导师。说起这些，才知道满涛是张可大姐的弟弟，贾植芳也是先生的亲戚。先生早年也喜欢俄国文学，给我们看了他那时写的小说，散文诗一般的优美，湖北农村泥土的馨香里面，掺杂着一股子契诃夫式的忧伤。我想，也许，正是这种没有说出来的、感觉方式和思维方式的同一，是我们之间心灵亲近感的来源。 <br />
　　五 <br />
　　在人与人之间，心灵的亲近比观点一致重要。有些人一见面就能信任；有些人交往几十年，依旧知面不知心。这个差别，和立场观点无关。反之亦然，心灵的亲近，并不意谓着观点没有分歧。 <br />
　　说到我的入狱，先生教我以平常心待之。说它只是人生一页，已经翻过去了。我说不，是翻过去了的一页又翻回来了。但我的体验已经不同。从前在夹边沟，虽然与世隔绝，总觉得由于自己的价值观，我必然地和文化人类保持着某种看不见的联系。现在，我已经不这么看了。 <br />
　　先生提到第一次见面，我曾把个体存在的意义，归结为同某种普遍性的联系。我说是。但我没找到这个价值本体。我说，所谓文明的内在结构，不也就是一个和价值本体的联系吗？如果除了通过外在的、人为的途径就找不到联系的线索，如果这线索只不过是舞台角色和道具之间的配合，所谓价值就成了虚拟的坐标，就像西西弗斯的石头。我说我有时候觉得，所谓意义的追寻，就像是被那个石头推着走，比之于我推它，更加要不得。 <br />
　　先生说，这可是虚无主义呀。 <br />
　　我说不是主义，是自然。就像人生的无常一样，我不得不与之面对。 <br />
　　先生说，这只是你一时的想法，说不定你还会改变。 <br />
　　我说是，思想是活东西，我只能听其自然。 <br />
　　这是临走的前一天的对话。那天，他请了一位烹饪高手，来家里为我们做了一顿特别丰盛的晚饭。饭桌上还点上了蜡烛。 <br />
　　六 <br />
　　转眼十几年，一直没有和国内的朋友联系。同先生，也只是通过在香港的王承义先生（先生和张可大姐的公子），偶尔报个平安。好在中美之间，时有共同的熟人往来，情况并不隔膜。听说他仍每天读书写作，不断有新作品出来。很欣慰，也很感动。听说他已“重评五四”，观点略趋中道，我想这是好事。思想的发展变化，正是它生命力的确证。八十高龄，依然和时代潮流同步，更难得。这不仅是先生永不老去的探索精神使然，也和他宽厚仁慈的天性有关。 <br />
　　九十年代中国学术思想的主流，已经由主张和平进化，反对激进变革，发展到重评历史。从崇尚英美模式，否定法国模式，发展到认为没有五四运动更好，没有辛亥革命更好。我漂流异国，久居山野，日与草木鸟兽为伍，已经落后于这个潮流很远。纽约一家杂志的记者远道来访，问我对这些问题有什么看法，我竞答不上来。只能说，我没有那样想过。 <br />
　　百年不过一瞬，但是人生几何？ <br />
　　采访录发表以后，很多人又骂我极端。困惑之余，不免要想，如能有机会再次向先生请教，深入讨论一下这些问题，该有多好。 <br />
　　何日归舟横怒海，苍颜白发叩师门。</div>

							
							
							
							
