<br />
关于版权、著作权诸问题，本也应属考虑、尊重之列，无奈于当今之“合法”文字不得通行，仅此“歪道”尚可有望扩大传播，惟祈诸位读者也能支持花城版以及将要出版的繁体版，但凡有能力购得者勿忘多多传播正版《寻找家园》。<br />
