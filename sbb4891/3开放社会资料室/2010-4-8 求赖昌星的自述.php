　　FT中文网特约撰稿人：当年，海内外都在传，你给了董文华房子车子，很多钱。有没有这样的事情?<br />
　　赖昌星：没有。我觉得舆论对董文华不公平。就说房子的事情，湘江花园的那幢别墅，那一天，董文华夫妇来看我，在场还有另外的一个朋友，董想买一个单位，当时开发商的销售，如果在香港付美金，就可以挑一个好位子，不然就不行。董文华问我，能否帮她在香港交美金，她在国内给我人民币，这是朋友间的帮忙。我当然答应，董文华也在北京把八十万人民币的买房钱交给了我，她丈夫都在场。<br />
　　FT中文网特约撰稿人：也就是说，董文华的房子是自己买的?<br />
　　赖昌星：是呀。结果那个房子给没收，真的对她不公平。我跟董文华是朋友，那时候远华有些项目开张，邀请她来演出，给她报酬，她都不要，因为是朋友帮忙，她就是那样的人。她甚至曾经问过我，做生意周转急需钱的时候，她可以帮忙。外边的传言太离谱下流，我跟她是一种患难时可以彼此关心帮助的朋友，她会关心我，我也会关心她。<br />
　　FT中文网特约撰稿人：你为何会跟她那么投契?<br />
　　赖昌星：这也是逐渐的，跟她交往四五年，感到彼此合得来做朋友，没有坏心。有时候，厦门酒吧开业，是政府的，请她来演出，另外有几次，大都是政府公关。我还是强调，那个房子，我只是帮她在香港垫了钱，她还了钱，因此，房子应该还给她，才公平。<br />
　　【关于曾明娜】<br />
　　FT中文网特约撰稿人：许多人关心你的婚姻关系，你跟曾明娜离婚的时候，是你被遣返可能最浓的时候，因此，海内外都揣测你是假离婚，主要是转移财产，如今你们是否仍然来往?<br />
　　赖昌星：她是小孩子的妈妈，我们当然经常往来。如果说为了转移财产，那倒未必，因为她和孩子们也有被遣返的可能。主要还是曾明娜觉得太有压力，或许离婚让她可以远离我的聚焦点，她提出，我尊重她，就同意了。不过，我们还是经常吃饭，也讨论孩子的前途问题。<br />
　　FT中文网特约撰稿人：既然是离婚，还是人当中年，人有七情六欲，那当然无法排除双方感情各自发展的可能性，你们之间有没有过任何承诺，比如一个不再嫁，一个不再娶?<br />
　　赖昌星：(踌躇很长时间)家里的事情，我不愿意公开谈很多。经济上我还是支持孩子们的学习生活等开销。你问我现在的感情生活，我也很难说。总之，我对自己控制得很好。如果真的有人追我，我会很好处理，现在很难讲，太高难度的问题。<br />
　　FT中文网特约撰稿人：另外，有报道说，你在卡加利有一个红粉知己，你还曾经去看望过她，是否实有其事?<br />
　　赖昌星：绝对没有。而是在我要被遣返回去的时候，卡加利有一个我都不认识的年轻女性，给一个美国记者说，她要到温哥华来送别我，要跟我照张相留作纪念。她就跟那个记者来了，我都不知道。记者怎么报的，我不清楚。后来我去卡加利，碰到高山的律师，提到那件事，记者就捕风捉影，说我去看她。我可以对天发誓，没有那回事。<br />
　　FT中文网特约撰稿人：你人生大风大雨，大起大落，是否有些新的领悟?<br />
　　赖昌星：不瞒你说，我自己觉得，我的人生过得很完整，很丰富，有钱也有过，也风光过，惨也惨过，赌命也赌过，穷也穷得惨不忍睹过，很完整，坐牢也坐过，有多人没有我这样完整的经历和感觉。我真的在中国为穷人，尤其是家乡的穷人做过一点事，这不是假的。我觉得自己没有做过大的坏事，偷税漏税是有的，今天中国的生意人，或者说世界上的生意人，谁没有偷税漏税，大小的问题而已。我是属于大的，今天中国许多有钱人赚了大钱，谁去回报社会?<br />
&nbsp; &nbsp; FT中文网特约撰稿人：你有没有想念家乡?<br />
　　赖昌星：有啊，以前在福建做生意，逢年过节，都请家乡父老吃饭，现在也会托人请家乡老人吃饭，也支持家乡孩子读书。现在当然少了，家乡来人，也不怎么见面，但常有打电话回去问候家乡的人。<br />
　　FT中文网特约撰稿人：你说你不是坏人，但你的案件牵动全世界，那你的孩子是否也是这样看你的?<br />
　　赖昌星：我不愿意让孩子知道许多案情的内幕，他们都很乖(注：赖昌星三个孩子，分别27岁，25岁，23岁)，也很支持我。我要大孩子去考地产经纪的牌子，我有那么多朋友，大家照顾一下，给他工作的机会，赚钱纳税，不很好吗?他们在加拿大住了十年，已经很适应这儿了。我也盼望他们在这里成家立业，以后可以回去中国看看。<br />
　　FT中文网特约撰稿人：你虽然拿到工作签证，但是，你仍然有可能会被遣返回中国坐牢，虽然可以避免死刑，但至少也是无期徒刑，在这样今天不知道明天的情况下，你是否是过一天和尚撞一天钟?心态如何?<br />
　　赖昌星：朋友们说我心态还平静，其实是我什么都想过了，最坏的都想过了，我就不去想了。有时候，我也会很害怕，但我就告诉自己，最坏的已经想过了，也就是没有命，什么样的情景都在心里描述过了，就不去想了，因为不是我自己能操控的。那次已经启动遣返(指06年5月18日，赖在机场头撞大柱，血流如注)，机票都买好了，那多可怕。后来延后，可以出来几个小时，我就高兴了。凤凰台的人说，我知道不会被遣返，其实我哪里知道，那是千钧一发。<br />
　　FT中文网特约撰稿人：听说你还是和不少朋友来往。一般而言，你风光的时候，可说是宾客盈门，你落难了，当然就树倒猢狲散，避之唯恐不及，你的情况如何?<br />
　　赖昌星：我身边还是新朋友多，都是在加拿大交的朋友。我总想，加拿大给我机会，我还会东山再起。赚了钱，就好好纳税。<br />
　　FT中文网特约撰稿人：海内外都说，你卷走了很多钱，现在还是名车豪宅，山珍海味，生活奢侈，是不是这样?<br />
　　赖昌星：没有。我现在是上午起的晚，中午晚上深夜，都要吃一点自己做的地瓜稀饭咸鱼，我一生都是如此。朋友来，我会下厨做好菜给他们吃，自己还是地瓜稀饭咸鱼。其余都是上网，见朋友，偶尔出去上馆子。<br />
　　FT中文网特约撰稿人：身体状况如何?<br />
　　赖昌星：我不锻炼，身体还不错，就是血糖高一点。<br />
　　FT中文网特约撰稿人：2008年，中国遭遇雪灾、地震，听说你也捐款了。<br />
　　赖昌星：我看电视，地震情况很惨，我捐了两次款。一次捐得较少，有一个执法部门的人说，这不像你的风格，我就又捐了一次，多一点，不过，我现在经济上也很困难，不可能像当年那样捐助社会。媒体采访我，我就说，有钱人就应该多捐钱，回报社会。我想，这些国内都知道。奥运，我也是关心，看得很多。我一直是爱国的，有些人办我的案子，我不服气，但国家的进步，我从来不否认――尤其是北京奥运，那么多的人拿金牌，海内外华人都高兴。<br />
　　FT中文网特约撰稿人：我认识一个牧师，说你已经去教会，是否这样?<br />
　　赖昌星：是的，我去教会。但并非每个星期日都去做礼拜。但我没有受洗。我有我的想法，要是受洗了，媒体就会报道我以前犯了罪，做了许多坏事，怕下地狱，所以受洗。我心里相信，但没有受洗，这里法庭社会，都用圣经，教会里的人也很友善，有爱心，教会里有一个地产老板，就常常要我去他公司做事，但因为他公司有两个洋人，我怕事情交待不清楚，做不好，一直在考虑，没有决定。<br />
 <br />
 <br />
&nbsp; &nbsp;【关于远华案】<br />
 <br />
　　FT中文网特约撰稿人：既然你承认偷税漏税很多，按照官方数字，达到五百亿元人民币，远华案办你，也没有什么错，你不觉得吗?<br />
　　赖昌星：偷税漏税是有，可以罚款，也可以做点牢。但说我腐败贿赂官员，这是个别人定的办案方向，是错的。我都可以说清楚，给谁的钱是怎么回事，我可以回去公开说。现在还有人主事，我的案件就结不了，他们是将错就错，办下去。案子扩大化，是一个事实。不少人死得冤枉。<br />
　　FT中文网特约撰稿人：你的“红楼”，成了腐败官员的典型教材，你又怎么看?<br />
　　赖昌星：我当时做生意，很辛苦，早上10点到晚上10点，好几个工地要巡视，根本没有时间花天酒地。我在福建，生意确实做得很大，朋友与各方关系的应酬也很多，我想应该大方一点，搞个红楼，请客吃饭，菜比外面的餐厅高档，服务也周到一些，同时也可以控制自己不在外面。我生意做得很大没错，那是凭我的聪明和胆量，并没有靠哪个官员给我撑腰。<br />
　　FT中文网特约撰稿人：你的意思是，走私集团并不存在，是你自己做大了，偷税漏税太多?<br />
　　赖昌星：我没有骗银行贷款，我没有卷走老百姓的钱，我的案子跟高山卷走银行存款根本是两码事。我走私，当年的体制就是有漏洞，体制难道没有问题?能让我崛起那么快?国家管理有问题，而不是我收通买人。中国老百姓如果认定我是腐蚀干部，拉人下水，然后捞取私利的小人，我觉得有点冤枉，但是，如果他们认为我大规模走私，偷税漏税，我也认。<br />
　　FT中文网特约撰稿人：你这样说，是否是认为你钻了体制的漏洞?你的案子给中加关系带来很多麻烦，有人说，中加旅游目的地协议没有签下来，赖昌星案件就是障碍，加拿大业界损失达上百亿。如果中加关系改善了，你是不是很紧张?怕被遣送回去?<br />
　　赖昌星：我也希望加中关系好起来，我现在是相信加拿大的司法制度会给我一条生路，我五十多岁了，看到孩子们因为我的事情不能正常生活，还是很不安的，如果我不被遣返，我就想把远华案的是是非非都抛诸脑后，绝口不再提，因为没有内幕可讲，而是干活赚钱，为孩子们创造一些条件，让他们生活的好一些。</div>

							
							
							
							
