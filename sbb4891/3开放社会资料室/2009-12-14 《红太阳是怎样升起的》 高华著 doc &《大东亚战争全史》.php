2002年8月5日。律师朱元涛乘坐的港龙航班由香港抵达北京，开箱检查时，《红太阳是怎样升起的》一书被宣布“境外出版物，依法予以没收”。<br />
2003年1月1日，朱元涛向北京市第二中级人民法院提起行政诉讼，状告首都机场海关。6月19日，一审宣判。法庭认为，根据有关法规，进出境旅客对其携带的行李物品，有依法向海关如实申报并接受海关查验的义务。海关有权对入境旅客携带的印刷品作通关查验，也有权对通关旅客携带的印刷品是否属于禁止进境物品进行审定。首都机场海关在扣留朱元涛携带的《红》书后，依程序请示海关总署，在得到批复后，对该书所作没收的行政处罚决定，认定事实清楚，适用法律正确，程序合法，应予维持。<br />
朱元涛一审败诉了。<br />
6月25日，朱向北京市高级人民法院提起上诉。他认为，一审判决注重了对首都机场海关涉案行政行为<br />
2003年8月7日，二审开庭，北京市高级人民法院对此案进行了认真的实体审查。2003年9月8日，北京市高级人民法院宣布终审判决：撤销北京市第二中级人民法院(2003)二中行初字第59号行政判决书；撤销首都机场海关2002年12月17日作出的《行政处罚决定书》。<br />
在判决书上，对于一审另一个没有正面回答的“是否走私”问题，高院做出了回答：“就本案而言，朱元涛携带的《红》书是否属于法律、法规规定的禁止或者限制进出境管理的范围，是确定其是否构成走私的前提。”法庭认为：“根据《中华人民共和国行政处罚法》第三十九条第(二)项的规定，行政处罚决定书应当载明违反法律、法规或者规章的事实和证据。而首都机场海关所作的《行政处罚决定书》，认定上诉人朱元涛携带《红太阳》一书属于禁止进境的书籍，没有具体引证该书属于违反《中华人民共和国海关对个人携带和邮寄印刷品及音像制品进出境管理规定》的哪种情形。<br />
事实上，朱元涛后来又托朋友从香港顺利地带回了同样一本《红》书。</div>

							
							
							
							
