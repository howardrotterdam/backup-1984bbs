													<h2>《中国向何处去？》怀念杨小凯</h2>
						
						
																											<div id="postmessage_63978" class="t_msgfont">提交日期：2009-3-20 8:38:00 <br />
作者：期盼革命:怀念杨小凯<br />
<br />
杨小凯先生逝去已经2年多了,我还在这里怀念他，可见并不是跟风赶时髦，而是发生内心的追思。<br />
　　 杨小凯去世的时候，我还不知道这个人。还是去年开始看制度经济学教材时，书中经常提到他的名字。我才关注到他。<br />
　　 再后来，粗读了杨小凯的《专业化和经济组织》、《经济学：新兴古典与新古典框架》，了解了他的学术思想，才知道他是最被世界认同的华人经济学者。世界知名经济学家称赞，杨小凯的研究使亚当.斯密的劳动分工论与科斯的交易费用理论浑为一体。张五常说，他是最有希望获得诺贝尔经济学奖的华人。<br />
　　 可惜，他离开了我们，只有55岁，正是一个学者成就事业的黄金时期。<br />
　　 我怀念杨小凯，不仅是因为他令人眩目的学术成就，更为他那对真理执着的追求和对社会无尽的关怀，正是因为这样，构成了他的良知，令人敬佩的良知。他坐过十年冤狱却自学成才；他没有上过大学却创立了国际性学派......关于他的经历，人们已叙述得太多，我不想再赘言。我只愿重温他生命中那些让我感动的片断，以怀念他令人仰止的学术品格：<br />
　　 ――1966年“文革”爆发，时年17岁的中学生杨小凯在大肆捕人之际，写出了《中国向何处去？》，主张中国实行巴黎公社式的民主政体，轰动一时。也引起了“文革”高层的注意。厄运开始降临，1969年11月，他被以反革命罪被判处十年徒刑，押往洞庭湖畔的岳阳建新劳改农场“服刑”。<br />
　　 ――铁窗十年，杨小凯拜当时关在牢里的二十几位教授、工程师为师，读毛选、《资本论》，学习了英语、机械、经济和数学。读罢《资本论》，杨小凯“心中期望未来的我成为一个经济学家”。与生俱来的质疑精神和个人经历使他并不相信流行的政治经济学，在没有西方新古典经济学的训练之下，他开始了与世隔绝中的自由思考。日后，杨小凯回忆说，“以前我会思考什么是好，什么是坏，这世界应该怎样。在监狱我想的是：这个世界究竟会怎样发展？”在长达十年的监禁生活里，杨曦光做了五六十本读书笔记，还有一个电影文学剧本。这些材料中包含很多与当时主流意识形态不相容的东西。<br />
　　 ――1978年4月，杨小凯刑满释放时已是而立之年。没有一个单位敢录用这位著名反动文章的作者，杨小凯在湖南新华印刷二厂当校对工。1979年杨小凯报考中国社会科学院经济学实习研究员，但因其历史，被拒绝参加考试。1980年他再次报考，在当时的社会科学院副院长于光远的帮助下，他终于获得参加数量经济学考试的机会，后被录取为实习研究员。实际上，在那个地方没有人瞧得起他。也没有人管他，他被迫独来独往。尽管杨小凯展示了他过人的才华，但由于没有正规文凭，社科院仍不能正式安排他的工作。<br />
　　 ――1982年，杨小凯被武汉大学聘为助教，教授数理经济学课程。在武汉大学期间，杨小凯出版完成了《数理经济学基础》和《经济控制理论》两本著作。他估计的一些计量经济模型未能在国内引起反响，却获得了当时来武大访问的普林斯顿大学教授邹至庄的注意。1983年，在邹的安排下，杨小凯被普林斯顿大学经济系录取为博士研究生。伴随杨小凯十几年的厄运在这一年才算最终结束。他没有选择学成回国，从此永远留在了外面。<br />
　　 ――在祖国一直不合时宜的杨小凯，在随后的岁月中，在远离家园的地方，如鱼得水。而且，他没有做一个纯粹的经济学者，而是始终关注中国的现实问题，提出了众多观点，如开放户籍制度、破除行业垄断、允许土地自由流转等等。一位密友说，“小凯仍然十分关心‘中国向何处去’。”<br />
　　 事实胜于千言。这就是杨小凯，面对黑色恐怖的威胁，敢于发出良知的声音；处于艰难困苦的牢狱，没有自甘沉沦，放弃对真理的求索；忍受了种种不公平的对待，却仍然深深爱着他的祖国和人民。记得伏尔泰说过，是真理光芒指引我们前进。不仅是学者，一个有良知的人，都应将追求真理视为人生的使命。杨小凯是就是一个有良知的人。在他身上，体现了湖湘学人矢志不渝的坚贞气质，心忧天下的使命意识，拯民水火的济世情怀。<br />
　　 我怀念杨小凯，不仅是对他个人的追思，向他远在天国的高尚灵魂致以深深的鞠躬，我更祈望，我们这个历经千辛万难的国家，不要让杨小凯那样有良知、有追求的人，再去承受不应有的屈辱和不公。</div>

							
							
							
							
