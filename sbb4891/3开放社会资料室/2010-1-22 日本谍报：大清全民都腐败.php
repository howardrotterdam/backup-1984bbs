													<h2>日本谍报：大清全民都腐败</h2>
						
						
																											<div id="postmessage_445380" class="t_msgfont">1889年4月，荒尾精向日本参谋本部递交了乐善堂间谍们的第一份重要成果：有关中国大势的分析报告《复命书》。<br />
<br />
这份报告认为，清国的“上下腐败已达极点，纲纪松弛，官吏逞私，祖宗基业殆尽倾颓。”而中日两国“唇齿相保、辅车相依”，在列强虎视眈眈下，清国一旦不保，日本势将进退维艰。因此，“清国之忧即日本之忧也”，日本要先发制人。<br />
<br />
此后由驻华间谍们提供的一连串大势报告中，最鲜明的就是强调中国的全民腐败，而不只是官场腐败，然后强调日本必须先改造中国，才能团结中国对抗西方。<br />
<br />
在这方面，分析得最为深刻，是宗方小太郎。甲午战争爆发后，宗方小太郎对其上一年草就的分析报告《中国大势之倾向》进行了全面的修改。<br />
<br />
他首先分析中国的年财政收入仅有9074万两银子外加523万石米（作者注：清廷在1893和1894年两年的财政收入均在8300万两左右），以中国之大，这是很不般配的。据他实际调查，民间的实际税赋是此表面岁入额之四倍，而且此类“定额以外之收入，一钱不入国库，均为地方官吏所私有。”他认为，中国历来贿赂之风盛行，地方官肆意刮削民众膏血，逞其私欲。因此雍正帝时始设养廉银，随官位之尊卑于俸给之外另行支给，拟以此杜绝其收敛之弊。但滔滔之势，非区区制法所能禁遏。时至今日，“‘养廉’二字实已辜负雍正之深意，官吏舞弊之势日见加剧。”<br />
<br />
当时东西方均有不少人看好中国即将崛起，坚信以中国之丰富物产，如能积极变革，则“成为世界最大强国，雄视东西洋，风靡四邻，当非至难之也。”但宗方小太郎对此不以为然，认为这是“见其形而下未见其形而上者”，观察一个国家也和观察人一样，应当先“洞察其心腹”，然后再“及其形体”。他眼中的中国虽然表面上在不断改革和进步，但“犹如老屋废厦加以粉饰”，经不起大风地震之灾。当时中国最著名的改革者和国务活动家李鸿章就自嘲为 “大清朝这座破屋的裱糊匠”，可算是两人有共同语言。<br />
<br />
究其原因，宗方小太郎认为腐败的基因遗传自明末，全民丧失信仰，社会风气江河日下，“人心腐败已达极点”，虽然有过康乾年间的短暂改革，但并未坚持下去，政府将本应建立“至善”人心的精力消耗在了“形而下之事”上。<br />
<br />
当时清廷之腐败规模几乎是全局性的。朝纲解钮时的宣统三年（1911年），作为“中央机关事务局”的内务府，其一年支银预算高达1024万两，而在咸丰朝仅为40万两，足足增加了24倍（宣统三年八月十一日度支部会奏）。至于冒领公款、挥霍浪费甚至侵吞赈灾款粮等更是层出不穷。两广总督岑春煊巡视陆军学堂，光一次宴会就“动需洋酒千数百金”，为此还专门在衙门中设立“洋酒委员”一职（光绪三十三年六月十五日法部主事梁广照奏）。甚至连军队都大量参与走私贩私，号称世界八强的北洋海军，其内里的腐败也是造成甲午一战全军覆没的主要原因。大量晚清小说均有对此的细致描写。<br />
<br />
宗方小太郎叹息，中国本来是有信仰体系和精神支柱的，那就是孔孟之道，但这一建国的基础却变成了科举的材料、当官的阶梯而已，知识分子汲汲营营地钻研此道，无非是以此为个人私利服务。当了官掌了权之后，就把孔孟之道抛在脑后，“得其位不行其道，而谋营私者也。”从中央到地方，庸官俗吏献媚当道，清廉高洁之士多不容于时流，官场一片漆黑，“朝野滔滔，相习成风”。官场腐败更导致上下隔阂，中央政府即使要施行仁政，也因为地方官吏的“壅塞”而导致“美意不能贯彻至民间”，好经都被歪嘴和尚念坏了，而少数有理想有抱负的人，只好在“愤懑不平”之余“退居闲地”，名望日高，隐然形成一股力量。所以，他认为中国是“有治国之法而无治理之人”。<br />
<br />
在 “普天之下莫非贪官”的晚清，以至于损害国家利权的一些架构，倒在客观上减少甚至基本杜绝了这一领域原先泛滥成灾的贪腐现象，如此结果，亦是中国近代史的吊诡之处。如，将海关税务交洋员团队（其最著名的领导人是赫德）管理，无疑是主权旁落，但洋员管理下的海关却成为政府中最廉洁也最敢于对腐败动真格的一个机构。美国传教士丁韪良（W Martin）认为，洋员管海关本是“叛乱（指挥太平天国运动）的私生子”，乃权宜之计，但正是廉政延长了这一怪胎的生命。他记述了在英国人李泰国（Horatio Nelson Lay）担任首任总税务司的几年内，上海道台多次设下陷阱想拉其下水，比如当两条商船一起到达口岸时，上海道台就建议李泰国，双方一人一条船私吞其关税，但始终不能得手。丁韪良说，海关洋员们的廉洁因此成为中国道台的眼中之沙，从而对这一税收体制怀有深深的敌意。<br />
<br />
宗方小太郎深刻地指出，国家是人民的集合体，人民是国家组织的一“分子”，“分子”一旦腐败，国家岂能独强？中国的“分子”们集体腐败，国家的元气就丧失消亡，这比政策的失误还要可怕，政策的失误尚且可以扭转过来，而国家元气的腐败就“不易返回中道”了。这位日本间谍引用孟子的话为中国下了断语：“上下交征利，则国危”，尤其是官场腐败导致司法不公，甚至使刑罚乃至性命都可以被金钱所左右，普通百姓申诉无路，民怨积压给国家带来巨大危险。<br />
<br />
宗方小太郎估计，以当时的形势来看，早则十年，迟则三十年，中国“必将支离破碎呈现一大变化”。而就在他写完这篇文章后仅十六年，武昌一场仓促而粗糙的革命就居然引起了连锁反应，几乎是瞬间将一个老大帝国毁灭。<br />
<br />
形势如此艰难，当政者理应“正纲纪、戒骄奢，排解地方政治之紊乱，消除民庶之怨薮，大施加仁惠，休养民力”，打造和谐社会，但宗方小太郎更吃惊地看到，政府却因惯性和麻木，束手无措，“因循支绌”，根本不在乎民心向背，并大搞为太后祝寿等形象工程，“粉饰太平”。<br />
<br />
这样的分析，出自一个间谍之手，也可见日本的间谍实在并非只是战术层面的，而是战略层面的。</div>

							
							
							
							
