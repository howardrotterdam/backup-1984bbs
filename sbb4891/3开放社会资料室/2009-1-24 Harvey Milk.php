Harvey Milk演讲的时候说：“要是子弹打穿我的头颅，让它先打穿所有的柜门”。这句话是在70年代的旧金山讲的，是对万千噤声不言的同性恋者讲的。意思是说，大家要勇敢站出来争取自身权利。Milk于1977年当选旧金山市参事（city supervisor），算是以公开同性恋身份登上高位的第一人。然而1978年11月27日他就被刺杀了，两颗子弹打穿了他的脑袋。和他一起死的，还有市长Moscone。<br />
<br />
刺杀Milk和Moscone的人叫做Dan White。White当年32岁，虽然年轻，但也是市参事，算是Milk/Moscone的对手。White在越南服过役，回来后当过警察和消防员，政治上属于保守派。旧金山从来就是嬉皮、同性恋者的大本营，White的仕途自然不顺――77年当选，78年就递交了辞呈。但是辞职不久他就改了主意，想找 Moscone要回位置。Moscone当然不干。几次商量未果，White掏出手枪解决了Moscone。之后，他走到Milk的办公室，顺带打死了 Milk。<br />
<br />
事情本来这样就结束。但是White的辩护律师开了一个史无前例的玩笑。他说第一，White杀人前极度抑郁和焦虑；第二，White杀人之前吃了大量的垃圾食物，导致抑郁和焦虑被激化；第三，White掏枪是出于警官本能的冲动。这个辩护方案被戏称为甜品辩护（Twinkie Defense），后来被禁掉了。<br />
<br />
结果White只被关了5年就假释出狱。假设死的单单是市长Moscone，5年应该不够吧？这么说来Moscone也够倒霉的，摊上了个同性恋一起赴死，冤都要少申一点。旧金山的4街上，有一个以Moscone命名的巨型会展中心，好歹算是一个纪念――当然Moscone本人是反对建造这个会展中心的。<br />
<br />
时代杂志把Milk选为20世纪100名英雄之一。有人不服气，说Milk做的那些事没什么，倒是White成就了他。这话道理不假，但是说这话的人都该先去被成就一下，看看有没有人记住您。70年代，同性恋依然被视为疾病，自愿发生关系的同性依然可能被关进监狱。Milk说：同性恋者“不可见”。都躲在柜子里，谁来为你说话？从在Castro街开相机店，到当选市参议，Milk传递出这样一个讯息：公开的同性恋者和大多数人一样，可以生活得诚实、正直；自己的权利，就要自己来争取。他能依靠的，只是这样一小群人，这群人长期被践踏，习惯了在底层忍气吞声，或者向下，寻找更底层的乐趣以逃避现实―― 即使是在斗争的时刻，也多有犹豫不决的情绪。但是Milk站出来了。他对他的男朋友说，我在市政厅从不坐电梯，因为大理石的楼梯更加宽敞。<br />
<br />
Milk活着的时候无法做成的事，他的死做成了。千百层柜门从里面推不动，需要子弹来打穿它，而Milk生前一直在找这颗子弹。所以他这样死，尽管可惜，却仍是死得其所。<br />
