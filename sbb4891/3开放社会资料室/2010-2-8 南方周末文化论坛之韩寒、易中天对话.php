<br />
韩寒：还在审查。<br />
<br />
易中天：有人不怀好意地说，这是你的营销策略，制造这个悬念，把账就赖在咱们政府头上。<br />
<br />
韩寒：事实上是这样。这个刊物啊他没有要那些投资方的投资，是我自己在掏钱。然后每个月要花将近……就是说人员的成本、办公的成本大概需要十多万。对我来说其实拖延没有任何的好处。到时候我连员工的工资都发不起，到时候只能去卖艺了。所以，真的只有我在办这个刊物的时候是进行过自我审查的。就是经过了自我审查了以后我认为没有问题。因为我从来也不是一个反党反社会的人。我只是想把它出版。但是后来我发现人们审查的尺度都不一样。可能有人觉得这个有问题，很多人一看觉得这个…会不会要承担一些责任啊，所以就没有人敢让它出版。<br />
<br />
易中天：这么说起来，那问题在这个地方。那么，是审查本身就不对，还是那个审查的人不对？<br />
<br />
韩寒：事实上可能都不对吧。因为我是赞同有一部分的审查的。这些审查主要是面对，一个是真的反人类的东西、反社会的东西，就是说我要去做人肉炸弹啊，我要去QJ一个女的啊，我要去做什么啊……我非常赞同把这些资讯是屏蔽掉的。但是据我所知，我在飞机上看到一个报导说在法国还是德国有一个互联网上有一个法规出台，就是说要禁止任何……关于儿童色情类的内容。事实上这个，我想大家都应该同意，而且在那个国家里大部分人包括网民大家都表示很理解。但是最后这个没有通过。是因为他们害怕有人会利用这些东西最后把这些审查扩大到一些思想领域的一些审查。所以我想说其实关于这本杂志啊，到最后，到这样，我相信，可能大家都有一些……因为很多人觉得，包括这个出版我是有问题，因为现在的出版社是国有的，然后呢，有一些出版公司，他们事实上要出版图书只能去买书号。我这个东西是交给出版公司做的，他们就去买书号，书号很便宜，一万块钱一个书号。但当出版社的人看到这个东西他们不会为了这个利益去承担任何的风险，所以对于他们来说，哪怕把这个，哪怕拿一本《最小说》给他们看，他们都可以看出一些……好多东西不能过啊。就到了这样一个程度。<br />
<br />
易中天：那就是说这个审查的人他因为不愿意承担这个责任，对他没有……他既不会因为出版韩寒的刊物就升官，也不会因为出版韩寒的刊物就发财。但是出了事，板子要打在他手上。<br />
<br />
韩寒：对，而且事实上最关键的呢，是我们又没有这么一个标准，比如……。因为没有这个标准以后呢，就会很困难，比如说我今天还好好的，但我不知道什么时候突然说话的时候没有控制住自己，就有谁看到不乐意了，但之前的那些作品、那些东西呢都要遭殃，然后那个人就会被连带进去。所以对于他们来说是多一事不如少一事的。那我想在对于一个在做文化的人，很多的人，很多很多，相当大一部分比例的人，都在想，唉，我要做一个关于文化的内容，我的原则就是多一事不如少一事。那这样一个国家其实离文化大国就差很远。<br />
<br />
易中天：那么由此引出的一个问题就是，什么叫做言论自由。言论自由在我看来有一个很重要的前提就是必须允许错误的言论也有自由。如果错误的言论是没有自由的，正确的言论才有自由，那其实是没有自由的。因为你永远弄不清楚哪个是正确的，哪个是错误的。这个正确的还是错误的是谁来定。<br />
<br />
韩寒：是，因为这个世界上如果没有批评的话，那赞美就没有任何意义。<br />
<br />
易中天：现在听说是这个重庆在控制，在卡拉OK歌厅里点低俗的歌曲会报警，有这个事，所以后来这个，你唱了低俗歌曲之后那个卡拉OK厅里面就呜啊呜啊呜…让你永远不可能低俗，因为你没有低俗的兴致了，呜啊呜啊呜…，我低俗一下，低俗不下去了。后来北京有个媒体人叫何东就写了篇文章说北京正在治理那个电动自行车，那个电动自行车很讨厌，他介于那个摩托车和自行车之间。你如果是摩托车像我们厦门是禁掉的，因为交通不安全嘛。那么自行车是可以走的。它属于两者之间它就，北京就不知道该怎么办。把它当摩托车禁吧，它不是摩托车，你说把它当自行车放了吧，它比自行车又太多了，撞人。所以何东就出了个主意说以后那个电动自行车前面都装那个录音机，唱红歌，比方说《血染的风采》啊。<br />
