<br />
易中天：他们该唱那个“把我们的血肉筑起我们新的长城。”<br />
<br />
韩寒：……因为当时我们扼杀了摩托车嘛，我们禁摩了，事实上这又是很多老百姓必须的一个交通工具。所以就不得以有了电动车……<br />
<br />
易中天：我们厦门摩托车还有好多。我投禁过一票。但是有些东西还得禁。你不禁的话，他危害，当然……<br />
<br />
韩寒：但是事实上我觉得，每个人都有使用这个道路的权利，哪怕是死在这个道路上，这就是交通的代价，是没有办法的。我相信，就像土方车一样……但是没有办法，他就没有禁土方车。但是土方车也一样，上海每天在以撞死一个人的速度……再这么下去就变成尸博会。<br />
<br />
易中天：我看你博客，你看《孔子》了？<br />
<br />
韩寒：我真的看了。<br />
<br />
易中天：感觉？<br />
<br />
韩寒：我其实当时也是为了避免一些口水战。就是我不想让那些孔子的那些崇拜者、追随者来说这些话，就没有评论孔子，我只是评论这部电影。因为这部电影从电影的角度来讲，是非常非常差的一部电影。无论是编剧啊，或者导演啊，都是在电影上说是不入流的。所以我当时就没有说这个话。但是后来我发现，其实还真的是中国有很多的很多的人会拿这些东西来说事，他们就会说，你这样说是玷污老祖宗，是玷污我们的圣人。<br />
<br />
易中天：谁这样说？是你这样说还是电影这样说？是骂你还是骂电影？<br />
<br />
韩寒：骂我。其实我觉得也是骂电影。因为我觉得很奇怪，如果那些人真的是把孔子当成是一个圣人，是一种信仰的话，那他们怎么可能容忍把孔子拍成一个电影，这令我觉得很奇怪，就像……<br />
<br />
易中天：孔子就根本不能拍电影？<br />
<br />
韩寒：对啊，就像如果我把他当圣人，当然我心里不是，那那些人就可能……（怎么也不可想象他们会容忍）那些电影会拿票房去说事……就像假设《孔子》的票房赢了，这些人说我们赢了！我觉得这个一方面跟孔孟之道差得有点距离，另外一方面你想在那些伊斯兰国家他们不会拿穆罕默德来拍电影，他们觉得不允许<br />
<br />
易中天：他们连电影都不允许。<br />
<br />
韩寒：然后包括在西方国家你肯定不能拍一个耶稣的故事啊，包括在印度你不能拍我佛的什么故事啊，释迦牟尼的故事啊。所以我觉得很奇怪，如果真的把他当一个信仰的话，怎么会去容许胡玫去把他胡搞成一个电影呢？和她的老公啊，和一些人一起。<br />
<br />
易中天：那现在这个孔子的改编电影是很热，除了胡玫的这部电影以外，还有两部电影。<br />
<br />
韩寒：所以我其实特别不喜欢这样的电影。这样的电影是扼杀人的想象力的。有一部就行了，我很害怕它获得成功。因为怕它获得成功，以后庄子、孟子、老子、孙子……都拍一遍。<br />
<br />
易中天：咱们现在文化输出啊。要做文化大国。文化大国没有出口肯定是不行的。文化大国并不能自己在家里牛逼……<br />
<br />
韩寒：根据我对现在的年轻人的观察啊，我发现事实上他们的想象力是足够的，他们的创造力是足够的，但是完全地被束缚了。有的时候你可以去看看网络上的那些帖子啊，那些小聪明啊，已经很多了，可能还现在还不足以去汇聚成一个很大的聪明。但是他们显示出这种潜质了。我相信如果要把文化的禁锢、文化的审查放得更宽一些，可以创作的作品更多一些，终会有现代的，属于中国自己的那些文化可以向国外去输出的东西，而不是那些孔子啊，那些孔孟之道啊哪些东西。那些我觉得一方面，包括那些在网上叫嚣的人，老祖宗的东西都忘了，但事实上孔子的传人到现在还在台湾，……说了一句话，我这辈子再也不会大陆了。<br />
<br />
易中天：我觉得他们是这样考虑的。我们要做文化大国，因此呢我们要有文化输出，那么我们要输出什么呢？我们输出的东西他要获得输出的成功必须有条件就是它必须是艺术，就是跟人家不一样的，你如果是人家有的，你还往人家那儿输出，人家说我们这里够了，我必须是个新鲜玩意儿，不同的东西。想来想去，大概就这个，所以拍电影。……同学不算数的，同学是教汉语的。他们很多不明白同学是干什么的，有同学是讲儒家文化的，儒家文化……就是告诉你一二三，韩寒两个字怎么写。<br />
<br />
韩寒：所以我觉得你说的这个，的确是，特别重要的一个问题。他们肯定在想这个，我们到底输出什么。那些……（普世的、）人类的那些东西别人已经输出过一遍了，再输出就输掉了，不是输出了。但是我觉得也不能拿孔子去输出啊。<br />
<br />
易中天：你觉得拿什么输出呢? <br />
韩寒：我觉得最近两年就别输出了。我觉得咱就先培养一下。输不出去的时候没有办法，现阶段的确输不出什么。那就只能输掉了。<br />
<br />
易中天：你是说在输不出的情况下我们就认输。<br />
<br />
韩寒：对，你总得认输，因为现在的确是输不出，真的输不出。<br />
<br />
易中天：可是我们现在站起来了。<br />
<br />
韩寒：是，当时因为当时有人说了一句嘛，有人在一个城楼上说了一句“中国人民从此站起来了！”<br />
<br />
易中天：他现在还在城楼上呆着呢。<br />
<br />
韩寒：但是呢，中国人民从此站起来了以后呢，没想到是往后走了几步，然后就再也没有地方坐下来了。我觉得这也是一个问题。所以我们，站起来了以后，包括一种情绪，这种情绪很奇怪，可能真的是跟中国的传统文化有一些关系，但是在全世界范围内可能也或多或少有些这样的情绪，比如说像大学，可能在座的每一位同学都会私底下说厦门大学那个不行，那个不大好，这个要改进，但是突然有南开大学、复旦大学的说你们厦门大学这个不好那个不好的时候，马上就（鼓起拳头、挺起胸膛）。所以我觉得有的时候必须……<br />
<br />
易中天：就像你在家，跟你爸如何如何，这、这、这……大学就像咱爸一样。<br />
<br />
韩寒：所以当时，在去年的时候，我有一篇文章，当时大家不是流行去抵制家乐福嘛，当时我就写一篇文章说到过，有关的内容，母亲啊，祖国啊这些解释……就说你为什么要去反对那些学生去上街去爱国啊，祖国是你母亲啊。我说谁说祖国是我妈啊。它不是我母亲，我母亲是我母亲，我母亲姓周。<br />
<br />
易中天：我们回到刚才那个问题……对你母亲没兴趣了……站起来，站起来了以后，你说要坐下来，人家想的是要走出去。现在我们要讨论的问题是，第一是我站起来了，如果没有站起来就不存在坐下来和走出去的问题，<br />
<br />
韩寒：视觉上站起来了。<br />
<br />
易中天：从哪个角度说视觉上站起来了？<br />
<br />
韩寒：我觉得从文化上还真的是……<br />
<br />
易中天：文化上站起来了。<br />
<br />
韩寒：因为写东西是坐着写的，哪能站着写呢？<br />
<br />
易中天：我明白了。就是我们现在不是要走出去，而是坐下来。<br />
<br />
韩寒：是的，我觉得现在就两种状态，一个是站着写，义正言辞，然后觉得自己站在什么样的一个高度上。然后呢还有一种方式就是跪着写，写一些跪着写的内容。但是很少有人是在坐着，正儿八经、正常地写。如果中国可以有一个很好的一个坐着好好写的一个氛围，那我相信不需要多长时间，按照中国人的聪明智慧，五年，十年……我相信，我不知道能够输出什么，但我相信一定会找到可以输出的东西，而不是现在这些东西。<br />
<br />
易中天：好，非常精彩，谢谢韩寒。</div>

							
							
							
							
