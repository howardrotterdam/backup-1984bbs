&nbsp; &nbsp; 在公元第五世纪初期，圣巴特瑞克把基督教带到爱尔兰，然后传播到苏格兰，再从北方返回英格兰。在第六世纪后期，教皇格列哥里(伟大者)派传教士由南部进入英格兰。在同一个世纪，英格兰人再一次皈依基督教。<br />
