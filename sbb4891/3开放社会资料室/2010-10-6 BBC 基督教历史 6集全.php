1、上帝<br />
<br />
上帝是唯一的最高的神圣的存在者、人格神、绝对完美的终极实在<br />
三位一体：同一个上帝具有三个位格：圣父、圣子和圣灵。<br />
惟有耶和华他是神，除他以外，再无别神 --《旧约・申命记》4<br />
<br />
神的本质为一，称为 上帝，是无始无终，无形无像，不可分开（无部分），全能，全智，全善的，是一切有形无形之万物的创造者保存者；但他有三位，同质，同权，又同属永恒，就是父，子，圣灵。<br />
<br />
---- 《历代基督教信条・奥斯堡信条》<br />
<br />
2、原罪<br />
罪的本质：人拒绝服从神的旨意，不让神决定他一生路程，而决意选定走自己的路。<br />
原罪要素：知识上不信与骄傲；意志上有与神同等的欲望；感情上放纵与放任<br />
<br />
3、拯救（Salvation）<br />
上帝的独生子化身成人，以其在十架受死替人赎罪，克服人离弃上帝的罪况，使人与上帝重新修和。<br />
<br />
4、上帝之国和末日审判<br />
“我实在告诉你们。财主进天国是难的。我又告诉你们，骆驼穿过针眼，比财主进上帝的国还容易呢！”（马19）<br />
<br />
5、启示<br />
上帝本身、上帝的意志和上帝的话语自我显示并在人间的传播。启示是上帝的恩赐。基督徒根据启示认识上帝和信仰上帝。<br />
