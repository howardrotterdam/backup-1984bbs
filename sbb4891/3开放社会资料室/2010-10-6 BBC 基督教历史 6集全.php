&nbsp; &nbsp; 这千年的基督教历史之问，将由Diarmaid MacCulloch 迪尔梅德.麦克库洛赫教授在本系列节目中不懈地探讨。<br />
<br />
&nbsp; &nbsp; 基督教基督教，是一个相信耶稣基督为救主的一神论宗教。基督教、佛教、伊斯兰教是世界三大宗教，估计现在全球共有15亿至21亿的人信仰基督教，占世界总人口25%-30%。最早期的基督教只有一个教会，但在基督教的历史进程中却分化为许多派别，主要有天主教（中文也可译为公教、罗马公教）、东正教、新教（中文又常称为基督教）三大派别，以及其他一些影响较小的派别。中文的“基督教”一词有时被用于专指基督新教，这似乎是中文的特有现象。是世界三大宗教之一，在全世界有约21.4亿信徒，为拥有信徒最多的宗教，以亚洲、非洲的信徒的发展最快。从476年罗马帝国分裂至1453年东罗马帝国灭亡。基督教分裂为罗马天主教和东正教（1054年）马丁・路德1517年加尔文。英国建立了独立于罗马教廷的英国国教会（圣公会）<br />
&nbsp; &nbsp;&nbsp;&nbsp;罗马书》 1：20 自从造天地以来，神的永能和神性是明明可知的，虽是眼不能见，但藉着所造之物，就可以晓得，叫人无可推诿。<br />
《罗马书》 1：21 因为他们虽然知道神，却不当作神荣耀他，也不感谢他。他们的思念变为虚妄，无知的心就昏暗了。<br />
《罗马书》 1：22 自称为聪明，反成了愚拙，《<br />
罗马书》 1：23 将不能朽坏之神的荣耀变为偶像，彷佛必朽坏的人，和飞禽走兽昆虫的样式。<br />
《罗马书》 1：24 所以神任凭他们，逞着心里的情欲行污秽的事，以致彼此玷辱自己的身体。<br />
&nbsp; &nbsp;&nbsp;&nbsp;基督教发源于公元1世纪巴勒斯坦地区犹太人社会，并继承了犹太教耶和华上帝和救主弥塞亚（根据希腊文翻译为“基督”）等概念，以及希伯莱圣经为基督教圣经旧约全书。<br />
&nbsp; &nbsp; 基督教的创始人耶稣(Jesus)。出生在巴勒斯坦北部的加利利的拿撒勒，母亲名叫玛利亚，父亲叫约瑟。基督教发端于1世纪巴勒斯坦地区犹太人的生活和信仰环境。<br />
<br />
1至5世纪基督教创立并从以色列传向希腊罗马文化区域<br />
<br />
313年，君士坦丁大帝颁布米兰诏书，基督教成为罗马帝国所允许的宗教。391年，罗马皇帝狄奥多西一世宣布它为国教。<br />
&nbsp; &nbsp;&nbsp;&nbsp;按照基督教经典的说法，基督教的创始人是耶稣，他30岁左右（公元一世纪30年代）开始在巴勒斯坦地区传教。耶稣声称，他的来临不是要取代犹太人过去记载在旧约圣经的律法，而是要成全它。耶稣思想的中心，在于“尽心尽意尽力爱上帝”及“爱人如己”两点。耶稣出来传道，宣讲天国的福音，劝人悔改，转离恶行。他的教训和所行的神迹，在民众中得到极大的回应。这使得罗马帝政下的犹太教的祭司团大受影响，深深感到自己地位不保，所以要把他除之而后快。后来由于门徒犹大告密，罗马帝国驻犹太的总督彼拉多将耶稣逮捕。耶稣受尽打骂侮辱，最后被钉在十字架上而死。但耶稣的心意却是为了要赎世人的罪，甘愿地流出自己的血。<br />
<br />
&nbsp; &nbsp; 依据他门徒们的见证，耶稣死后第三天从石窟坟墓中复活了。他的坟墓空了，他又多次向满心疑惑的门徒们显现。他们渐渐确信耶稣真的复活了，是胜过死亡的救主。在耶稣升天超离这世界的时空后，他的门徒们起来热心宣扬耶稣的教训，并且宣告他是复活得胜死亡的主。信徒们组成彼此相爱、奉基督之名敬拜上帝的团体，就是基督教会。耶稣复活的这一天成为后世的复活节（每年春分以后、又逢月圆的第一个星期日）。教会又定了12月25日为耶稣的生日而则成了圣诞节（耶稣出生的确实日子已不可考）。耶稣出生的那一年被后世定为公元纪年的元年（但因计算错误，耶稣实际出生的年份应为公元前六年或四年）。<br />
&nbsp; &nbsp; 部分非基督宗教教徒的史学家认为，耶稣传达的教义更加接近犹太教，而保禄（又译保罗，Paul）对基督教教义做了较大贡献，使得基督教不再局限于犹太人范围。<br />
