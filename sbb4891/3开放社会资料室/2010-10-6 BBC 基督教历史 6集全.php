1) 认为耶稣只是基督教神话中的偶像；<br />
(2)耶稣是错误传说中的人物；<br />
(3)基督教中的耶稣实际上是历史上同名者的捏合杂揉；<br />
(4)在同时代的历史著作和考古发掘中找不到充分的根据；另外，教会中同样有一部分人否认耶稣有血肉之躯，他们认为耶稣基督是神，作为历史上有血有肉的人的耶稣只是一种&quot;幻影&quot;，或者说是人的幻像。<br />
&nbsp; &nbsp; 主张耶稣实有其人的既有护教人士，也有非基督徒的学者，各种看法之间往往大相径庭，大致可分为如下几种<br />
1) 全盘接受圣经的记载，维护教会传统，认为耶稣就是道成肉体的基督；<br />
(2)耶稣是当时犹太人&quot;叛乱的首领&quot;；<br />
(3)耶稣是一场革命运动的领导人；<br />
(4)耶稣是个大骗子；<br />
(5)耶稣是至死不悟的预言家；<br />
(6)耶稣是有血有肉的普通人，但後来的人们将他神化了。学术界对圣经中&quot;拿撒勒人耶稣&quot;这个名称展开过讨论。<br />
&nbsp; &nbsp;《新约》头5卷称耶稣为&quot;拿撒勒人(Nazarene)或&quot;拿佐拉派。(Nazoraean)；而在犹太教文献中，耶稣及其门徒被称作&quot;拿撒利姆&quot; (Notarim)。大多数学者认为这些词都与加利利一个叫做&quot;拿撒勒&quot;(Nazareth)的城市有关，但也有少数学者不同意这种看法，他们认在《旧约》中找不到这个拿撒勒城的名字，可能它是一个小村庄名字。近人罗伯逊(ArchibaldRobertson)引述4世纪约有关献後指出:公元前在叙利亚和巴勒斯坦境内就有一个教派叫&quot;拿撒利派&quot;(Nasaraens)，并说:&quot;另一桩奇事是，一个叫做曼达安派的教派，他们崇奉施洗者约翰为真先知，而斥责耶稣是说谎者和骗子，但是依然称他们的主要成员为拿撒拉(Nasoraye)也就是&quot;拿佐拉派&quot;。他因此得出结论说:&quot;约翰和耶稣的信徒们被称为拿佐拉派。但是这个称号并不是源於拿撒勒的村名，却是源于希伯来语Natzar一词，原是'守'的意思即指守秘密者或严守犹太律法者。&quot;国内也有人认为基督教是由上述拿撒勒派发展而来。似乎更多的学者坚持拿撒勒派是基督教中的一个派别的观点，认为该派出现的时间应为1、2世纪。<br />
&nbsp; &nbsp;&nbsp; &nbsp; &quot; 耶稣&quot;在希腊文作lesous，是希伯来文Jeshua(耶书亚)。Jehoshua(约书亚)，Joshua(约书亚)等词的译音，原意为&quot;耶和华 (是)拯救&quot;。罗伯逊认为希腊文中&quot;基督?耶稣&quot;就是&quot;弥赛亚?约书亚&quot;，同《旧约》中的先知约书亚有内在联系。但这种一家之言忽略了&quot;约书亚&quot;这词本身的含义，也撇开了《马太福音》第1章第21节的解释，所以不能为大多数学者所接受。<br />
&nbsp; &nbsp; 即使非常肯定耶稣实有其人的论者，对耶稣的生平仍存有种种疑点，比如耶稣的生卒年代，照教会的传统说法，耶稣生年就是公元纪年的开始，但是历史学家指出: 《路加福音》第2章第2节所载&quot;居里扭作叙利亚巡抚的时候&quot;，应在公元6-7年，然而按《马太福音》第2章第1节的记载&quot;当希律王的时候，耶稣生在犹太的的佰利恒&quot;来看，生年应定于公元前4年。不仅生年仍为悬案，卒年亦谁以确定。<br />
&nbsp; &nbsp; 《圣经》中记载耶稣生平事迹寥寥无几，童年的事几乎末提到，只在《路加福音》第2章讲到他12岁时跟父母去耶路撒冷，在圣殿里坐在教师中间听讲发问的情景。然後就是第3章第23节说:&quot;耶稣开头传道，年纪约有30岁。&quot;此外，福音书记载的都是他传道以後的言行。所以黑格尔也觉得不无遗憾地说:&quot;关於耶稣[思想]发展的成熟过程这一有趣的问题，我们没有得到任何消息。他最初出现时已经是在成年时期，那时他已经从犹太人传统旧俗念意识中解放出来了。&quot;<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;综合现有的资料和研究成果来看，关于耶稣生平有下列几点我们可加以肯定:<br />
1.耶稣是犹太人；<br />
2.生活在公元1世纪初；<br />
3.30岁左右在加利利和犹太各地传教；<br />
4.曾收12个人为大弟子；<br />
5.遭到犹太教上层分子的嫉妒；<br />
6.後以&quot;谋反罗马&quot;罪被钉死於十字架。<br />
&nbsp; &nbsp;&nbsp;&nbsp;&quot;基督&quot;一词在希腊文作&quot;Christmas&quot;，意为&quot;受膏者&quot;，即希伯来文&quot;弥赛亚&quot;的对应词。在《新约》圣经中，常同耶稣连用或换用。但近代研究者常将耶稣同基督分开。有的还将&quot;历史的耶稣&quot;和&quot;信仰的基督&quot;加以区分。<br />
<img src="http://i3.6.cn/cvbnm/77/d5/a4/38cb8cfb7554a7d73539f471f2e0e772.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
信者得救的典故<br />
&nbsp; &nbsp;&nbsp;&nbsp;《路加福音》23:39-43 在众人不信耶稣的情况下，和耶稣同钉十字架的一名犯人，悔过并信了耶稣，他的罪被赦免，并得救。<br />
&nbsp; &nbsp;&nbsp; &nbsp;在起初的一二百年间，基督教是作为异教被政府（罗马帝国）禁止的，基督教信徒受到迫害，甚至被处死。直到313年，罗马帝国皇帝君士坦丁大帝颁布米兰诏书，从而使基督教成为政府所允许的宗教。325年，尼西亚信经在第一次尼西亚公会议上通过，并成为基督教的最基础的原则，现在几乎所有的基督教教派都接受这一原则。392年，罗马皇帝狄奥多西一世宣布基督教为国教。从此，基督教开始迅速发展起来。<br />
&nbsp; &nbsp;&nbsp; &nbsp;395 年，罗马帝国在内部矛盾和外族入侵下分裂为东罗马和西罗马，基督教也开始分裂为东正教（自称正教）和罗马公教（自称公教），两个教派都自称是基督教的正统。罗马教皇（教宗）是罗马公教最高领导者，而君士坦丁堡牧首则是东正教的首领。到1054年，东部教会和西部教会互相宣布把对方宗主教驱逐出教，标志着罗马公教和东正教正式分道扬镳，称为基督宗教大分裂。<br />
&nbsp; &nbsp;&nbsp;&nbsp;公元7世纪伊斯兰教兴起，耶路撒冷落入穆斯林的控制之下。西罗马地区的教会和国家为解放圣地耶路撒冷在1095年－1291年间先后八次十字军东征。<br />
&nbsp; &nbsp;&nbsp;&nbsp;16世纪，德国、瑞士、荷兰、北欧和英国等地发生了宗教改革运动，它产生出脱离天主教会的基督教新教教会。领导人物是马丁・路德、加尔文等人，他们建立了新教和圣公会，脱离了罗马天主教。中国所称的“基督教”，基本上都是这个时候产生的新教。<br />
&nbsp; &nbsp;&nbsp;&nbsp;基督是“基利斯督”的简称，意思是上帝差遣来的受膏者，为基督宗教对耶稣的专称。基督宗教是信奉耶稣基督为救主的各教派的统称。该教与佛教、伊斯兰教并称世界三大宗教。于公元1世纪由巴勒斯坦拿撒勒人耶稣创立。他是上帝的独生子，为圣灵感孕童贞女玛丽娅而降生；他曾行过很多神迹，让瞎子复明，跛子行走，死人复活，但是因为犹太公会不满耶稣基督自称为上帝的独生子、唯一的救赎主，把他交给罗马统治者钉死在十字架上；死后第三天复活，显现于诸位门徒，复活第40 天后升天；还会于世界末日再度降临人间，拯救人类，审判世界。被12使徒中的犹大叛卖并受难，受难日为星期五，最后的晚餐连耶稣有13人，所以在西方，13是人们忌讳的数字，并且与星期五一起视为凶日。<br />
&nbsp; &nbsp;&nbsp;&nbsp;尽管有三大教派，但是基本教义都是相同的。即上帝创世说，原罪救赎说，天堂地狱说。《圣经》，由《旧约全书》和《新约全书》两部分组成，是基督教的经典。十字架是基督教的标志。他们信奉的“上帝”或“天主”本体上是独一的，但是包括圣父、圣子、圣灵（圣神）三个位格。<br />
&nbsp; &nbsp;&nbsp; &nbsp;上帝神(God)：基督教信仰圣父、圣子、圣灵三而一的上帝。上帝是三位一体――圣父是万有之源造物之主，圣子是太初之道而降世为人的基督耶稣，圣灵受圣父之差遣运行于万有之中、更受圣父及圣子之差遣而运行于教会之中。但这三者仍是同一位上帝，而非三个上帝――三位格、一本体，简称三位一体。<br />
<br />
创造(Creation)：基督教认为上帝创造了宇宙（时间和空间）万物，包括人类的始祖。<br />
<br />
&nbsp; &nbsp; 罪(Sin)：亚当与夏娃在伊甸园中违逆上帝出于爱的命令，偷吃禁果，想要脱离造物主而获得自己的智慧，从此与上帝的生命源头隔绝，致使罪恶与魔鬼s身，而病痛与死亡则为必然的结局。后世人皆为两人后裔，生而难免犯同样的罪，走上灭亡之路。<br />
&nbsp; &nbsp;&nbsp;&nbsp;救赎(Redemption)：人生的希望在于信奉耶稣基督为主，因他在十字架上的赎罪，并他在三日后从死里复活，使悔改相信他的人一切的罪皆得赦免，并得到能胜过魔鬼与死亡的永远生命。<br />
&nbsp; &nbsp; 灵魂与永生(immortality)：人有灵魂，依生前行为，死后受审判，生前信仰基督者，得靠基督进入永生。怙恶不悛者，将受公义的刑罚与灭亡。世界终有毁灭的末日，但在上帝所造的新天新地中，却是永生常存。<br />
&nbsp; &nbsp; 1. 忌讳崇拜除上帝以外的偶像。向基督徒赠送礼品， 要避免上面有其他宗教的神像或者其他民族所崇拜的图腾。要尊重基督徒的信仰， 不能以上帝起誓， 更不能拿上帝耶稣开玩笑。基督教由于教派不同， 其各个教派的教条也有所不同，为了避免无意中损伤感情， 对一些问题一定要弄清楚。如： 神父与牧师是天主教与新教对其神职人员的不同称呼， 不可混为一谈。 <br />
<br />
2.忌食带血的食物。 <br />
<br />
3.忌讳衣冠不整。非基督徒进教堂应衣冠整洁， 进去后要脱帽。与人谈话应压低声音， 不得妨碍对方正常的宗教活动。当教徒们祈祷或唱诗时， 旁观的非教徒不可出声， 然而当全体起立时， 则应当跟随其他人一起起立， 若有人分饼和面包给自己， 应谢绝。<br />
 <br />
4.基督徒有守斋的习惯。基督教规定， 教徒每周五及圣诞节前夕（１２ 月２４日） 只食素菜和鱼类， 不食其他肉类。天主教还有禁食的规定， 即在耶稣受难节和圣诞节前一天， 只吃一顿饱饭， 其余两顿只能吃得半饱或者更少。基督徒在饭前往往要进行祈祷， 如和基督徒一起用餐， 要待教徒祈祷完毕后， 再拿起餐具。 <br />
<br />
5。 忌讳１３ 和星期五。另外， 他们讨厌“１３” 这个数字和“星期五” 这一天。在基督徒眼中“１３” 和“星期五” 是不祥的， 要是１３ 日和星期五恰巧是同一天，他们常常会闭门不出。在这些时间， 千万别打扰他们。<br />
