													<h2>原小说里面的惨烈场面</h2>
						
						
																											<div id="postmessage_549" class="t_msgfont">作者：郑义 <br />
<br />
　　一九六七年十月，地区的两派斗争已达到白热化状态。代表们正在中央办的学习班谈判，讨价还价。而在下面，双方正紧张地调兵遣将，准备抢占在政治上、军事上、经济上有重大意义的战略要地，造成既成事实，以取得谈判桌上得不到的东西。不久，造总兵团这一派的外围三县先后失守，井冈山这一派则已集结八县兵力，兵临城下。在这严重的情势下，北京的汇报会上，中央文革首长表示对我们两派的情况十分关切，并分别向双方旗帜鲜明地表了态：“造反有理！你们是左派，我们是支持你们的！”并重申了Jiangqing“九・五讲话”文攻武卫的原则：“当阶级敌人向我们进攻的时候，我手无寸铁，怎么行呢？”“谁要对我武斗，我一定要自卫，我一定还击。”根据北京来电，两派都编印了Jiangqing自七月底以来几次关于文攻武卫的讲话摘录，广为散发，因为双方都认为自己一方是左派，是革命造反派，是为维护毛主席革命路线而战斗的。大家决定，丢掉幻想，实行文攻武卫。 <br />
　　我们造总兵团作战部决定趁对方攻城部署尚未完成之机，立即拿下六中“文攻武卫”广播站。因为它象楔子一样，插进了由我们造总占领的城区。在战斗打响后，如果井冈山那一派往外一突，我们的防线就会腹背受敌，形成两面作战的困境。要是没顶住，撕开了口子，我们只有撤出整个中部平原，被人家挤到边远的西北一隅，其后果是不堪设想的！任务交给了青年近卫师。 <br />
　　师部开了半天会，吵得天翻地覆。最后师部李红钢来告诉我，说是决定叫我先去侦察一下。因为我这个美术教员从未参加过武斗，每天背着画夹到处画水彩，人们都以为我是一个逍遥派画家，其实我已经执行过几次侦察任务了。 <br />
　　我绕到学校侧面，小心地翻过围墙。墙内，几排高大的垂柳把柔软的枝叶一直拖到地上，和茂密的蒿草交织在一起，简直是人迹不到的原始森林了。我悄悄地摸过这密不透风的柳帐，一片奇异的景色突现眼前： <br />
　　大操场上长满野草，纵横着几道交通沟、战壕。几个新构筑的机枪火力点，互成犄角之势。主楼上弹痕累累，一面破碎的战旗在秋风中轻轻摆动，不时翻露出“文攻武卫”字样。两排红色的枫树球拥着主楼，在中午的秋阳下象火焰似地烨烨发光。而树下，密布着装满火药的大铁炉…… <br />
　　……钢笔稿很快打完了，我打开调色盒赶紧着色。转身就跑的念头紧紧缠绕着我，使人顾不得用心调色，各种单色毫无变化地抹上去。先用草绿盖上草地（我没敢画出战壕和机枪火力点，只是在草丛中做了记号，标出了位置），再用土黄把主楼平涂一遍，湖蓝的天，大红的旗，橄榄绿的柳树；最后涂上桔黄，稍稍调了点红，定了定心，细致地点画出那一树树火焰般的枫叶，我喜欢这火红的枫，每个秋天都要画的。而且，这树丛中，我标出了那些具有极大威慑力的大火炉。这对于战斗是至关…… <br />
　　“不许动！举起手来！”背后一声低沉的喝斥。 <br />
　　坏了！――我的心一下停止了跳动。等我还未反应过，手中的画夹已被夺走了。 <br />
　　“往前走！”――我只好撩开柳条，走出了茂密的柳帐。几个端着半自动步枪的年轻人将我围起来。 <br />
　　“做甚来了？”一个男孩子揪住我的衣领。看样子他最多是老初二的。要是过去，我声音高点，说不定还能把他训哭呢！他恶狠狠地骂道，“狗造总！我们还没死绝哩！”然后把刺刀往我脖子底下一晃。 <br />
　　“没画什么……风景画……是张钢、钢笔淡彩……”我紧张得答非所问地结巴起来。 <br />
　　“还不吐实话？娘的！你想死想活？”骂着，他掉过枪来给了我一枪托子。 <br />
　　“我没说半句假话哇！”我忍住疼，急忙辩解。一看他又瞪起了眼，我忙不迭地叫道，“最高指示：‘要文斗，不要武斗’！” <br />
　　“嗯，语录背得好熟！放开他，小兔子。”一个姑娘从背后慢慢走到我面前，“你再背一条：语录本第十一页，快点！” <br />
　　“革命不是请客吃饭，不是……” <br />
　　“怎哩不响气了？不是什么？快点！” <br />
　　“不是请客吃饭，不是做文章，不是绘画绣花……” <br />
　　“好了！”她喝住我，从别人手中接过画夹子，冲我扬了扬，说：“不是绘画绣花，不能那样温良恭俭让！革命是暴动――懂吗？” <br />
　　然后，她拿起画来，几个人围过去。 <br />
　　“看那枫树，好看啊！” <br />
　　“哈，画得挺美哩！还有咱们的战旗，看那红！” <br />
　　“连咱们楼上的标语都画上了：‘人不犯我，我不犯人，人若犯我，我必犯人！’” <br />
　　年轻人七嘴八舌地议论开了，连用刺刀对着我胸口的孩子也忍不住歪头想看看。我心里一阵轻松：幸好没急着走――色彩果然打了掩护！ <br />
　　那为首的姑娘抬眼审视着我，突然问： <br />
　　“画写生，为甚不如实画？为甚不画战壕、炸雷？” <br />
　　我想尽量顶住她的突然袭击，镇静地说： <br />
　　“破坏风景，不堪入画。” <br />
　　她眼睛里隐约闪过一丝温和的笑意，扭头对同学们说： <br />
　　“我押他上主楼去，你们还是去干你们的事吧！” <br />
　　我一听，愣了，苦苦哀求道：“小同志们，放我走吧！下次再不敢来了！这完全是误会呀！” <br />
　　姑娘一拉枪栓，把子弹顶上膛： <br />
　　“误会？误会也得说清楚！少废话，背上画夹，头里走！……东张西望做甚？别打算跑啊――嫌疑犯嘛，我还不敢把你打死，可是一梭子打断你一条腿总还是敢的哩！” <br />
　　完了！没指望了！我心里一凉：进了主楼，就不容易出来，即使盘问不出什么，今晚仗一打开，那还不是陪进去了！跑吧？不行，看来她真敢开枪。我只好战战兢兢地朝主楼走去。 <br />
　　沿着荒草没踝的林荫道，拐了两个弯，我们走到了楼前的枫林里。突然，这姑娘两步赶上来，把枪口一抬，轻声叫道： <br />
　　“王老师，您不认得我啦？” <br />
　　――我的学生？霎时，我又惊又喜，停住了脚步： <br />
　　“你是――” <br />
　　短发，男孩子似的短发，方脸盘，薄薄的嘴唇，神气的翘鼻子，散乱的额发下，一双稚气未脱的大眼，在树荫下闪动着骄矜的光芒： <br />
　　“六二年，您毕业刚分到咱们学校教美术，头一个班就是我们哩！” <br />
　　她见我仍然记不起来，就把头发向后抹了抹，说： <br />
　　“辫子剪了……卢丹枫。” <br />
　　丹枫！――想起来了：现在“文攻武卫”广播站的播音员，原高三丙班团支书，初三时我教过她几天。 <br />
　　“您真的是一直在逍遥，不是来侦察的？……什么地方不能画画儿，偏偏往这儿跑？这是什么时候！”她愤愤地责怪道。 <br />
　　我只好满脸堆笑，言不由衷地骗她： <br />
　　“快一年没回学校了，挺想的……秋天色彩丰富，是画风景的最好的季节：柳树还绿呢，杨树已经黄了。还有这枫树，看霜一打，都红透了……” <br />
　　“您不骗我？……那，我放您走吧！――您先顺墙根往北跑，听到我的枪声后，往东一拐就是柳树林……” <br />
　　看着她明亮真诚的大眼睛，我心里真有一股说不出的滋味：今天晚上，就要玉石俱焚了！我想告诉她点什么，但又说不出口――我们两派之间那种你死我活的争夺象泥封一样锁住了我的嘴。我只好说： <br />
　　“丹枫，听说人家最近要来围攻你们呢！” <br />
　　“嗳，早知道了！要奋斗就会有牺牲，怕死就不革命了！再说，我们已经把他们反包围起来了，打起来，没他们便宜占！”接着，她朝我扬扬手中的那张画儿，又说，“王老师，这张画您可不能带走了。如果您还要，我替您保存吧！”最后，她微笑着，满怀信心地说： <br />
　　“毛主席说：‘跨过战争的艰难路程之后，坦途就到来了。’――等我们红色政权巩固了，文化大革命最后胜利了，那阵啊，您想怎么画就怎么画吧！” <br />
　　我只好默默地转过身去，泪水都快冒出来了。 <br />
　　“站住！……王老师，您认得李黔刚吧？――听说他前些日子改名了：因为‘黔’字拆开是‘黑’‘今’，太反动，改成红钢了。――现在据说也算他们造总的一个头头。原来学生会的。” <br />
　　我点了点头。 <br />
　　而丹枫此刻却陷入沉思之中。她的目光缓缓转向头顶的枫叶，良久不语。忽然，她轻盈地纵身一跳，从头顶上摘下两片红透了的枫叶。她把枫叶举到眼前细细端详了一会儿，嘴角掠过一丝不易为人觉察的淡淡羞涩。 <br />
　　“给我捎个信儿，好吗？……” <br />
　　说着，她把枪往肩上一背，抓过我的画夹，嘴里抿着叶柄，在一张水彩纸上飞快地写了几行字。然后精巧地叠起，递给我： <br />
　　“别叫别人看见，亲手交给他……好吗？”她把枫叶从嘴上拿下来，又说，“把这也给他吧。”接着爽朗而略带羞涩地莞尔一笑。 <br />
　　这是两片火焰般的并蒂的枫叶。我接过来，和信一起放进我内衣口袋，我想起来了：先前李红钢申请加入青年近卫师那阵，就有人反对，说他和卢丹枫极好，而卢丹枫却是井冈山那一派的骨干。后来因他一直矢口否认，又加上作战勇敢，才正式让他进了近卫师。现在看来，还真有其事了。可是，今天晚上……我同情而忧郁地看着丹枫。 <br />
　　丹枫把枪下肩，轻轻打开保险。她微笑着默默地看着我，好象是说，好了，走吧！ <br />
　　我扭身便猛跑起来。 <br />
　　“站住！站住！”丹枫大声喊叫着。我跑得更快了。“哒哒哒……”枪声响了，一串子弹从我头顶飞过，打得枫叶乱飞。我记起丹枫告诉我的活，马上往东一拐，几步就窜进了柳林。浓密的柳条打着脸，讨厌的蒿草缠着腿，我不顾一切地尽力快跑。……突然脚下一空，我一头栽进了一个坑，几乎摔得昏死过去。我试图爬起来，脚却不听使唤了。背后己传来急促的脚步声。在这万分危急之际，我发现：这竟是一个打开了盖的暖气沟！什么也来不及考虑了，我一头钻进去。刚爬进去，外面已传来人语： <br />
　　“是不是钻沟了？没见人翻墙呀！” <br />
　　“我们在这儿守着，你回去拿手电。” <br />
　　我忍着剧痛，拼死往里爬去…… <br />
　　我精疲力竭地从暖气沟里钻出来，早已不是人样了：衣服全被管道卡住扯成条条。全身划满了口子，还光着脚，鞋也没了。谁要想画“逃亡者”，我就是最好不过的模特儿了。更为不幸的是，我发现，这竟是主楼！――没让人家抓回来，倒自己送上门来了！但我又不敢再钻下去，如果有人正在顺着爬过来找我呢？无奈，我只好盖上沟盖，溜进一间教室躲起来。 <br />
　　刚刚喘过气来，就听到几声低沉的爆炸声和激烈的枪声――他们等不上我，战斗开始了。我赶忙站起来，从窗户向外望去：学校前面的墙已被炸塌了好长几段。我们的人已经冲进来，但被大操场上那几个掩体里的机枪顶住了。人们被压在地面上，头都抬不起。掩护进攻的几挺机枪刮风似地叫着，没把人家火力压制住，只是打落了厚厚一层树叶。 <br />
　　战斗越来越激烈。我头顶上有挺机枪，因此我这一楼也沾了光：不用说玻璃一块不剩，连窗户扇也打碎了。我只好爬到北面的教室里。北面不会有战斗，这是我们给他们留下的一条退路。忽然，窗外有人说话： <br />
　　“丹枫，你们看，从北面出去，可能问题不大，翻过土墙就是居民区了。他们三面包围，留下北面，看来是想拔钉子，把咱们打出城去。你们找到方面军指挥部，问明白了：咱们这里，还守呀不守？把咱们的意见说清楚：咱们是插在他们心口上的一把尖刀，坚决不能撤！” <br />
　　“保证完成任务！走，小兔子！” <br />
　　我紧张地屏息倾听，两个人的脚步声渐渐远去。在北面，为了以防万一，我们也埋伏了一挺机枪。但愿他们能严守命令，不打突围，只打外援。漫长的几分钟过去了，没有枪声，我悬念着的心才放下来。 <br />
　　我躺在窗户根下，一些有关丹枫的事总索绕在脑际…… <br />
　　过去，丹枫是团干部，又是全校学毛选积极分子。文化大革命开始后，林彪发表《再版前言》，丹枫不但把它背熟，还在一次全校学毛选经验交流会上，从那个红本子上，把一条条语录背出来，讲她怎样“反复学习，反复运用”。为了解释“急用先学，立竿见影”的原则，她蹬地站起来，走到讲桌前，强烈的聚光灯，把她的影子投射在讲台上……台下传来会意的笑声。丹枫看了看自己直挺挺地站在台前，自己也忍不住笑了：“……可我并不是根竿子啊！”在满堂大笑中，“立竿见影”的原则和丹枫那笑盈盈的形象一起生动地植入大家的脑海。 <br />
　　后来，在一次批判会上，同学们揭发批判校领导抵制学毛选的伟大群众运动时，有人念了页码，叫他们背几段语录。可笑得很，书记、校长、教导主任，竟没有一个象点样，当场出丑。张校长还有点不服气，嘟囔了一声：要这么个检查法，谁也不行！丹枫从人群中站起来走到台上，把语录本往张校长手里一塞，说：来吧，随便哪一段！张校长愣住了。大家你一条我一条地点了十来个页码，不管是常用的还是生僻的，丹枫一律背得滚瓜烂熟。这把大会主席也惊呆了，他翻了半天，点道：二百七十一页二段。大家刷刷地翻着，接着一片沉寂。丹枫想了想，答道：语录本只有二百七十页，没有二百七十一页，更没有等二段……暴风雨般的掌声淹没了她的话尾。那一排当权派们，个个都低下了头…… <br />
　　主楼南面的战斗似乎已远去了，我无所事事地躺在水泥地上，莫名其妙地想起这些事。突然，北面传来一个长长的机枪连射。我心里一惊，猛地跳起来――我一耳朵就听出来了，这是埋伏在废水塔上的那挺机枪！ <br />
　　……一个背枪的年轻人，就是那个打了我一枪托的“小兔子”，抱着一箱子弹，从低矮的土墙上慢慢跌下来，一动不动了。墙上，刚刚出现了两只手，机枪又扫过去。那扒在墙上的两只手消失了，一切复归于沉寂。丹枫！她，也这样无声无息地完了吗？我颓然地躺倒了。 <br />
　　楼上响起一阵紧张的脚步声，人声，砸窗户声。看来是把南面的一挺机枪搬过来了――显然，有什么新情况。我又站起来：对面土墙根儿上，出现了一个大洞，不知是用刺刀还是用枪托弄开的。丹枫伸出手，抓住“小兔子”一只脚，把他拖进去。片刻，洞又扩大了。不一会，一团东西从洞里爬出来。我定睛分辨着，原来，丹枫左手挟着“小兔子”的脖子，右手在地上爬，低姿匍匐前进。显然，她是企图以怀中的尸体为掩护，越过这片被严密封锁的开阔地，传达指挥部的重要命令。霎时，所有的人都明白了。水塔上的机枪泼水般地扫射起来，我头顶上的机枪、步枪也都分不出点地打开了。他们想压住对方的火力、掩护丹枫。但水塔上的人根本不予理睬，死盯住丹枫不放，打得她周围一片烟尘。她，在艰难地爬行着，身后，两副接起的绑腿渐渐从洞里拖出两箱子弹和两支枪…… <br />
　　好！啊――终于爬到机枪打不到的死角了！等候在那儿的人们一拥而上，把她从地上扶起来。但她却跪倒在“小兔子”的尸体前，仔细地翻看着，猛然扑倒在尸体上放声大哭起来。 <br />
　　一股异样的感情猛烈地袭上心头，我看不下去，扭过头来…… <br />
　　南面，战斗在反复地顽强争夺中艰难进展。“文攻武卫”放弃了大操场上的前沿阵地，又把两座配楼上的人都撤回了主楼。他们缩短战线，集中兵力，但并不突围。――看样子，丹枫冒着枪林弹雨带回来的是坚守待援的命令。 <br />
　　既然放开一面他们不走，于是只好不客气，团团围定，攻击更加猛烈了。在几次小股轮番佯攻中，杀伤力极大的“炸雷”――大铁炉子被消耗掉了。近卫师的战友们浴血奋战，终于冲进了主楼。激烈的争夺战在楼里展开了。 <br />
　　……一楼的枪声，手榴弹爆炸声已经停息，战斗向二楼发展。我听着楼道里杂乱的脚步声，不敢贸然开门出去，怕一开门就挨一梭子。这时，我听见了李红钢低沉的嗓音，于是放开喉咙狂喊了一声：“李红钢！”脚步停下了。我打开门，一下冲出去。 <br />
　　李红钢一愣，扑上来使劲搂住我。大伙儿呼啦围上来：“哈哈，中午听见那阵枪声，还当你早就报销了呢，真是命大哩！”战场相见，生死重逢，这暖人心肺的战友之情使我热泪盈眶了。我还没来得及说句什么，不知哪儿打来两枪，一个同志倒下了，人们哗地散开。我拣起枪，和同志们一起向楼上冲 <br />
　　“井冈山”为了节约弹药，把早已准备好的水泥板，拆下来的暖气包和桌椅仪器，不分点地往下砸，问或还夹杂着手榴弹。我们的伤亡不小。复仇的愿望把每一个人的感情都激发得在燃烧！在爆炸！我们舍生忘死地冲击着，一层一层地冲上去。 <br />
　　五楼终于被占领了。还存下的井冈山那一派的人逃上了楼顶，连几个伤员都没来得及拖上去。从五楼通往楼顶的小天窗被他们拼死封锁着。手榴弹扔不上去，枪不顶用，我们又有了新的伤亡。 <br />
　　李红钢想了想，叫人找来个小炸药包，桌子叠桌子地把炸药包顶到楼板下，然后命令小天窗那儿的人继续猛攻，炸药包一下就掀开了两块楼板，露出个半间房的大洞。硝烟未落，李红钢身先士卒，大喊一声，一个翻身就跃上了楼顶。他一面猛烈地扫射着，一面高呼着： <br />
　　“近卫师的战友们，为了胜利，冲啊！” <br />
　　人们踏着烂桌子堆冲上楼顶。不到一分钟，战斗结束了，枪声已经停息。楼顶上井冈山那一派人有不少伤亡。他们再也不会从刺叭里用语录同我们唱对台戏；他们再也不会爬起来用机枪和手榴弹屠杀我们造总战士了。 <br />
　　不知觉间，竟已是黄昏了。每个人全身骨头都象散了架一样。大家都坐下来想喘口气。李红钢摸了摸我的枪管，咧开干裂的嘴唇笑道： <br />
　　“有点热乎了啊？咋说，王老师――开头不习惯吧？钢铁就是这样炼成的！” <br />
　　我含混地点了点头，困惑地想到：有谁知道我开第一枪时那新奇而又害怕的心情呢？我这握惯画笔的手，居然端起了枪，杀人！斗争就是在这样改变着人？ <br />
　　忽然，从楼角里慢悠悠地站起一个人，右手高举着两颗手榴弹，东摇西晃地向我们走来。――啊，拼命的来了！这个意外的情况把人吓慌了，大家不约而同，刷地卧倒一片。李红钢最先清醒过来，他跳起来把枪一举，厉声叫道： <br />
　　“放下武器！快――我开枪了！” <br />
　　那人站住了，高擎着手榴弹的右手也慢慢垂下来。她把头上的钢盔摘下来，随手一扔。――啊，那齐耳根的短发，那男孩子般的短发在晚风中微微拂动…… <br />
　　“丹枫！……”李红钢耳语般地惊呼一声，木雕泥塑似地呆住了。 <br />
　　丹枫没有回答，她把弹环从小指上褪下来，手一松，手榴弹掉在脚边。她缓缓走到李红钢面前，恨恨地责问道： <br />
　　“你为什么要来？为什么要来？为什么？……双手沾满井冈山人的鲜血――刽子手！刽子手！刽――子――手！……” <br />
　　她猛然双手抱头，踉跄着向后倒去。李红钢一步抢上前，拦腰抱住了她。 <br />
　　“丹枫！丹枫！你醒醒，你醒醒！”李红钢在她耳边焦急地呼喊着。 <br />
　　“黔刚，你还记得我？”丹枫渐渐苏醒过来，她疲倦地拢了拢凌乱的散发，微微苦笑道：“咱们这么见最后一面，也是当初所想不到的吧！” <br />
　　泪水浮上了她的眸子：“要是我能亲眼看到文化大革命的最后胜利，那该多好啊！”她一把揪住李红钢的胸襟，热切地说：“黔刚，你快清醒吧，快回到毛主席革命路线上来吧！你快点调转枪口吧，黔刚！” <br />
　　李红钢忍住泪水，背过了脸： <br />
　　“不！……你，你……投降吧！” <br />
　　丹枫愤然一挣，一把推开李红钢。她后退了几步，整了整血迹斑斑的褪了色的旧军衣，轻蔑地冷笑道： <br />
　　“至死不做叛徒！――胆小鬼，开枪吧！” <br />
　　李红钢――我们青年近卫师前卫团长，这个在枪林弹雨中腰都不猫的人，此时竟全身哆嗦开了。 <br />
　　“没有一滴热血！”丹枫感叹一声，扭身向楼边走去…… <br />
　　“丹枫！丹枫！！丹枫！！！”李红钢短促而惊恐地高叫着，手里的枪在剧烈地抖动。然而丹枫没有听见，李红钢的呼唤淹没了她那广播员的高昂的口号声中：“井冈山人是杀不绝的！共产主义是不可抗御的！誓死保卫毛主席的革命路线！誓死保卫毛主席，誓死保卫林……” <br />
　　在这最后的高呼中，丹枫跃出了最后的一步…… <br />
　　一片死寂。楼下传来一声沉闷的声响，象是一麻袋粮食摔到地上。 <br />
　　“啊――”李红钢歇斯底里的嚎叫着，把整整一梭子子弹射入晚霞绚丽的暮空。 <br />
　　大家一起扑上去，七手八脚下了他的枪，把他按倒在地…… <br />
　　……不知是哪个好心的人已经把她的身体顺直了，衣襟也拉好了。她躺着，静静地躺在一层战火摧落的枫叶上。晚风徐来，刮落几片如丹秋枫，飘洒在她青春饱满的胸上，飘洒在她没有血色的脸旁。我这时才记起她托我捎给李红钢的信和枫叶，连忙从怀里掏出来。信还基本完整，枫叶却早已揉得不成形了。我抬起头，想摘两片代替，但摘下许多，竟都不是并蒂的。我惊异了，仔细看了好久，才发现只有每根枝梢上的两片枫叶才是并蒂的。 <br />
　　我不知该用什么话来安慰李红钢。和他一起伫立许久，我才把信及并蒂枫叶递给他，说： <br />
　　“她叫我给你的……” <br />
　　“什么时候？” <br />
　　“刚才，中午。她放我走时。” <br />
　　李红钢小心翼翼地把信展开。昏暗中，我掏出火柴，划着……只见字迹朦胧： <br />
　　 <br />
　　黔刚：一切都好吗？想念你，又恨你！还记得咱们的最后一次见面吗？我知道你的心。我只是热切地盼望着胜利的那一天，在欢庆胜利之际，一切都会如愿以偿的！ <br />
<br />
　　火柴燃尽了。李红钢颤抖地把枫叶拿到眼前，呆呆地凝视着。见他看得那么专心，费劲，我又划着了一根火柴：这枫叶柄向上，颜色退晕地由橙变红，到了五个俊秀的叶尖，已红得象红玛瑙似的单纯、明朗、热烈。枫的细细的叶脉，在橙色的叶片上伸展着，宛若鲜红的血管。那般红艳，简直里面还奔流着生命的血液。啊，这经霜的红叶，竟如此动人，如此美！ <br />
　　李红钢掏出语录本，打开，拿出两片枫叶，也是并蒂的，只是早已干枯，色彩也暗淡了，失去了柔嫩的活力。 <br />
　　“一年多了，去年十月，运动开始不久，那阵造总和井冈山还没分成两派呢……”李红钢嘴唇嚅动着，自言自语地回忆道，“那天晚上，成了我们的最后一次谈心……后来，她摘了两片枫叶，递给我说：‘喂，给你’……分手时，她说：‘让咱们勇敢地投身到这场伟大的革命斗争中去吧，一起横扫一切牛鬼蛇神，一起为共产主义伟大真理而奋斗！’……连手儿都没拉过，我们在一起总是谈生活，理想，斗争……” <br />
　　秋风习习，枫叶瑟瑟。仿佛是听见了他俩在校园里的阵阵絮语，我也沉入那痛苦迷离的图画之中。 <br />
　　“喂……火，”李红钢又低声嘟噜了一句。我又划着了一根火柴，水彩纸上又显露出丹枫那娟秀的笔迹： <br />
　　 <br />
　　但是在你回到毛主席的革命路线上来之前，我们是没有任何个人幸福可言的！你应当反戈一击，尽快觉悟。否则，革命不是请客吃饭，不是绘画绣花，你会在战场上死在我的枪口下！ <br />
<br />
　　李红纲撒开手，信和枫叶打看旋转落在地上。他猛然半跪在丹枫身畔，泪水大颗大颗地跌在地上。我一下划着了几根火柴，在这明亮的一瞬，她依然如我中午所见那样英气勃勃。男孩子似的短发，方脸盘，薄薄的嘴唇，神气的翘鼻子，只是那一双稚气未脱的大眼已永远失去了骄矜的光芒。 <br />
　　李红钢轻轻地理着她额上的乱发，口中喃喃说道： <br />
　　“我没死在你的枪口下，你，你……你却死在……啊！” <br />
　　实在抑制不住了！他失声痛哭起来： <br />
　　“丹枫……丹枫……丹枫啊，啊！……” <br />
　　一片枫叶飘落在丹枫唇边，好象这是她在最后一次留恋地亲吻着晚风、爱情，亲吻着青春、生活，亲吻着她那永别了的一切！ <br />
　　一切都模糊了！一切！泪水充盈着我的眼眶。火柴燃尽，一切又都溶进了迷茫的夜色…… <br />
　　远处，传来隆隆炮声，“井冈山”开始攻城了。校园里响起集合的哨音。刚刚易手的广播站播送着鼓舞人心的语录歌：“发扬勇敢战斗、不怕牺牲、不怕疲劳和连续作战的作风。”极为严酷的保卫战开始了。 <br />
　　李红钢勉强站起来，用衣袖揩了一揩脸上的泪水。最后回望了丹枫一眼，踉跄地向集合的大火堆走去。 <br />
<br />
　　两年过去，经过几度反复拉锯，对立派终于掌权了。为了巩固夺得的政权，判处了一批“武斗元凶”。其中有两人是以把卢丹枫“扔下五楼摔死”的罪名判决一死一缓。又过了一些日子，学习班里揭发出李红钢，说丹枫是他用枪逼得跳楼的。这不属于战场上的人命，因为丹枫当时已经放下武器了。这样，人们就花了几个日夜的时间，终于把李红钢从外边找来，加以拘捕。原来当时他早已脱离造反组织了，在外边“逍遥”了很长一个时期，但是仍被判处死刑，立即执行。 <br />
　　那天，刑车从人群中驶过。我没有去看，我只是在一条静僻的路上漫步沉思。路的两边，枫树又红了，象一丛丛烧得旺旺的火。那火红的树冠，红得简直象刚刚从伤口喷射出来的血，浓艳欲滴…… <br />
<br />
原载《文汇报》1979年2月11日</div>

							
							
							
							
