													<h2>徐友渔：读阿伦特笔记</h2>
						
						
																											<div id="postmessage_7143" class="t_msgfont">人类在二十世纪遭遇的最大苦难来自极权主义，它肆虐于不同的国度，制造种族屠杀、饿死千百万人的饥馑、骇人听闻的镇压和逮捕、震惊世界的冤案与审判。探讨悲剧的起因成为一代杰出思想家的责任，他们的不朽著作具有跨世纪的启示性意义，其中，汉娜・阿伦特的《极权主义的起源》令人瞩目。 <br />
<br />
　　《极权主义的起源》一书由三部分组成：一、反犹主义；二、帝国主义；三、极权主义。作者认为，极权主义的起源和成分是反犹主义和帝国主义。作为犹太人的汉娜・阿伦特亲身体验过纳粹法西斯的暴政，她的沉思具有出自切肤之痛的实感；她曾在哲学大师海德格尔和雅斯贝尔斯门下学习，她的政治理论蕴涵了德国哲学的形而上学的思辨性和深度。这两个特点使得她的学说在二十世纪的政治思想家中别具一格，与此同时，也许是因为这两种特色过于明显，使得她在独具慧眼的同时，也暴露了相当大的盲区，忽略了一些显而易见的事实。阅读《极权主义的起源》一书对于我们是一个契机，在评判汉娜・阿伦特的回答是否周全和可靠的同时，促使我们思考极权主义的起源的重要因素到底是什么，二十世纪人类最大悲剧究竟起于何处。　　 <br />
<br />
　　反犹主义&nbsp; &nbsp;&nbsp;&nbsp;<br />
<br />
　　初看起来，本书的第一部分“反犹主义”是一部近现代欧洲反犹主义发生、发展的简史，完全可以成为独立的著作，作者在一些细节上的停留，对某些微妙之处的深入挖掘，使人感到她的兴趣是在反犹主义本身，而游离于分析极权主义起源这个目的。 <br />
<br />
　　应该说作者并没有偏离主题，在她看来，反犹主义是纳粹意识形态中的主要因素，犹太人问题和反犹主义首先成了纳粹运动兴起和建立第三帝国组织结构的触发因素，随后触发了史无前例的世界大战暴行，最后又造成西方文明亘古未有的种族灭绝。 <br />
<br />
　　一般人容易认为，反犹主义是民族主义发展到极端的形式，但汉娜・阿伦特指出，事情根本不是如此，反犹主义和民族主义不是一回事，现代反犹主义是在传统的民族主义衰朽时生长起来的，并恰恰在欧洲的民族国家制度被摧毁时达到了顶峰。 <br />
<br />
　　作者沿袭了托克维尔和伯克的敏锐观察：一般人最仇视的未见得是压迫者，而是丧失政治权力却保有财富的人，犹太人多半不关心政治，少数有政治地位的人随社会变迁被挤出权力圈。当犹太人丧失了他们在公共事务中的作用和影响、除了财产之外一无所有时，反犹主义达到顶峰。 <br />
<br />
　　汉娜・阿伦特的独特眼光和思辨能力使她发现一种吊诡的现象：对犹太人的社会歧视产生于犹太人与其他一切民族群体之间日益增长的平等。在封建时代，犹太人虽被隔离于社会，但由于理财经验、与欧洲贵族的关系，他们也有一定的特权。封建秩序的崩溃，使新的、革命性的平等概念得以兴起，这种平等使“民族中的民族”不再被容忍。条件越平等，人际事实存在的差异就越难得到解释；而因此个人之间和群体之间就更不平等。因此，犹太人的条件越平等，他们之间的差异越令人吃惊。这种新的意识导致社会嫌恶犹太人，但同时又被他们的奇特之处所吸引。 <br />
<br />
　　汉娜・阿伦特自身的犹太人身份和敏锐的感觉在她描述犹太人中的精英被同化时充分表现出来。同化即摆脱二等公民地位，被非犹太的主流社会接受，但他们被恭维说，他们是例外，即虽然是犹太人但不像犹太人，他们是被当成“人类的新人种”，当成“一切人都是人类”的人文原则的活生生的例证被接纳的。这对他们的心理状态有灾难性后果，这些新贵及其后代除了极力讨好别人还能干什么呢？ <br />
<br />
　　但是，正是这种敏感，这种对于反犹主义的某些复杂微妙方面的精细分析，妨碍了与主题的联系，反犹主义最后到底是怎么成为纳粹意识形态的中心，怎么促成了极权主义的统治，本书没有提供清晰的说明。比如，人们可以问，法国的反犹主义同样强大，而且还发生了臭名昭著、极为轰动的德雷弗斯案件，最重要的鼓吹种族理论的著作《论人类种族的不平等》也是法国人高比诺写的，汉娜・阿伦特自己也说，“直到纳粹自称种族精英之前，公开蔑视其他一切民族以法国的种族主义最一贯”，那么为什么法西斯政权和极权主义是在德国而不是在法国呢？　　 <br />
<br />
　　帝国主义&nbsp; &nbsp;&nbsp;&nbsp;<br />
<br />
　　探究极权主义的起源必须讨论帝国主义，汉娜・阿伦特说：“在帝国主义时代以前没有世界政治，而若没有世界政治，极权主义宣布要统治全世界就毫无意义可言。”更重要的是，极权主义的两个关键因素――暴民和种族主义是在帝国主义之内发生和发展起来的。 <br />
<br />
　　汉娜・阿伦特认为，帝国主义产生于殖民主义，是在十九世纪最后三分之一的时间里由民族国家制度与工业发展的不相适应而引起的，到了1884年左右，开始其为扩张而扩张的政治，终结于英国放弃对印度的统治。与我们熟悉的观点相反，她主张帝国主义是资产阶级取得政治统治权的第一个阶段，而不是资本主义的最后阶段。资产阶级开始只关心经济利益而对政治权力没有兴趣，在帝国主义时代，商人变成了政治家，在资本积累达到其自然的、民族的极限时，他们的信念是权力哲学，“扩张就是一切”。 <br />
<br />
　　虽然暴民是从资本主义组织过程之中兴起的，但资本与暴民的结合则是帝国主义政策产生之时表现出来的。只有帝国主义政治家才能利用暴民，只有种族理论才能煽动暴民。暴民组织采取将民族转化为种族的形式，这时人性的观念、人类团结的观念没有了，没有人类，只有种族，而种族之间只有征服与战争。“种族主义的确会给西方世界带来末日，因此也会给整个人类文明带来毁灭……从政治角度来看，种族不是人类的开端，而是终结；不是一个民族的起源，而是衰亡；不是人类的自然诞生，而是他的非自然死亡。” <br />
<br />
　　按汉娜・阿伦特的说法，帝国主义时期的某些基本方面显示出非常接近于二十世纪的极权主义现象，如果要说帝国主义孕育或导致了极权主义，那么难于解释的一个反例就是，英国维多利亚女王时代是最典型的帝国主义，但英国并没有滑向极权主义。汉娜・阿伦特没有明确地论及这个问题，她在英国式的海外帝国主义和德国、俄国式的大陆帝国主义之间作了区分。与海外帝国主义相比，大陆帝国主义的特点是：公开蔑视法律和法律机构，在意识形态上将无法律性合理化。 <br />
<br />
　　照这种说法，单纯的帝国主义未见得和极权主义有直接关系，要加上对法治的践踏才能与极权主义相联系。汉娜・阿伦特的论证思路大概确实如此，她说，有一种倾向将极权主义简单地等同于其成分和起源――似乎反犹主义或种族主义或帝国主义等每一种发生形态都可以说成是“极权主义”，这种谬误会导致在寻找历史真相时误入歧途，在作政治判断时得出有害结论。于是，她对极权主义的性质作了深入分析，对极权主义的特征作了生动刻画。 <br />
<br />
　　极权主义&nbsp; &nbsp;&nbsp;&nbsp;<br />
<br />
　　汉娜・阿伦特指出，极权制度是一种独特的政治制度，它摧毁一个国家所有的社会、法律和政治传统。 <br />
<br />
　　现代社会的危机使阶级制度和政党制度崩溃，将大多数人转变为一种无组织、无结构、由愤怒的个人组成的群众，极权主义运动就在这样的条件下兴起。在进一步的发展中，极权主义运动较少地依靠无结构的群众社会，较多地依靠分子化的、个人化的群众的具体条件。 <br />
<br />
　　掌握了政权的极权主义消灭社会的阶级结构和民族差别，制造一个分子化和个人化的社会。在这样的社会中，一极是最高元首如希特勒，另一极是无组织的个人，不存在公民社会，也没有实质性的社会中介阶层。&nbsp; &nbsp;&nbsp; &nbsp; <br />
<br />
　　极权主义政府不能用守法或不守法政府衡量，它实际上蔑视一切成文法，甚至走极端到蔑视自己制定的法律，它并不关心是否要废除法律，也用不着废除宪法或法律，而是完全忽视它们。 <br />
<br />
　　在专制下，人与人的政治接触被切断，人类的行动能力落空，但私人生活的整个领域，以及经验能力、建造能力都未被触动，而极权恐怖的铁掌不留这种私人生活空间。恐怖破坏了人与人之间的一切关系，逻辑思维的自我强制破坏了和现实的一切关系。 <br />
<br />
　　极权统治的基础是人的孤独，孤独与孤立不是一回事，孤立只涉及生活的政治方面，而孤独涉及整体的人类生活，它根本不属于世界的经验，这是人类经验中最彻底、最绝望的一种。&nbsp; &nbsp;<br />
<br />
　　汉娜・阿伦特对极权主义有许多独到、深刻的观察和剖析，比如她说： <br />
<br />
　　极权主义教育的目的从来就不是培植信念，而是破坏形成信念的能力； <br />
<br />
　　极权主义国家的规律是：凡是政府机构，越明显可见，权力就越小，越不为人所知，权力越大； <br />
<br />
　　极权主义缺少一个统治集团，这使独裁者的继任问题变得特别麻烦和为难。 <br />
<br />
　　不应有的忽视&nbsp; &nbsp;&nbsp;&nbsp;<br />
<br />
　　极权主义是把一些大国全方位席卷的重大历史现象，我们不可能想象，其起源没有经济原因。事实上，在《极权主义的起源》问世前几年，哈耶克名著《通向奴役之路》就对此作出了精到的阐述。汉娜・阿伦特即使不愿意重复哈耶克的话，至少应该在书中提及他的观点。她不这么做显然不是不知道，而是不认同。我认为，她对极权主义起源的分析漏掉了第一位重要的因素，这不能不是《极权主义的起源》一书的重大缺陷。 <br />
<br />
　　哈耶克在《通向奴役之路》中指出，高度集中的指令型经济将导致失去自由，指挥一切经济活动的当局将控制人们达到生活目标的手段，控制全部经济的人将控制一切手段，将决定人们应当争取什么，应当相信什么。个体所有制是自由最重要的保障，这不仅对有产者，而且对无产者也是如此，只是由于生产资料掌握在许多独立行动的人手里，才没有人控制我们的全权，我们才能够以个人的身份来决定自己要做的事情。 <br />
<br />
　　希特勒统治下的德国是汉娜・阿伦特的极权主义例证之一，其经济的国有化程度、集中和垄断的程度、按计划发展的程度，在世界各国也是相当高的。早在1931年，在“民族社会主义经济政策基本观点和目标草案”中，德国就提出推行中央集权的国家统制经济，强调“国家应通过对国民经济的监督和领导，确保民族共同思想及其统治”。而法兰克福学派成员纽曼认为，“今天的德国经济有两个广泛的、显著的特征，它是垄断经济，也是命令经济”。 <br />
<br />
　　汉娜・阿伦特虽然提到，在极权主义的意识形态中，它宣称自己严格遵从自然法则和历史法则，但她未能像卡尔・波普那样，由此出发而进一步指出历史主义和乌托邦社会工程论构成了极权主义的思想基石。卡尔・波普在其《开放社会及其敌人》中说，所谓历史主义，就是主张历史受控于明确的演化发展规律，这使得极权主义者得以宣称自己认识到了人类历史发展规律，从而实施按计划的、大规模的、彻底的社会重建工程，在这样的乌托邦社会工程中彻底控制和改造社会。《开放社会及其敌人》发表在《极权主义的起源》前几年，汉娜・阿伦特没有提到卡尔・波普的观点，看来也是不以为然。 <br />
<br />
　　卡尔・波普的观点具有明显的长处。比如，他提出，极权主义与人类文明自身一样古老，柏拉图的学说就是鼓吹极权主义的学说，而古希腊的斯巴达的一些主要特点和现代极权主义一致。汉娜・阿伦特则认为极权主义是西方文明内部在现代化进程中的产物，这就无法解释在这种时间、空间限制之外而主要特征一致的极权主义现象。 <br />
<br />
　　汉娜・阿伦特的分析还蕴涵，极权主义起源于现代西方民族国家的内在脆弱性，宪政民主、法治等内在缺陷，使得其追随者把产生于现代化和民主过程中的极权主义说成是现代化和民主的产物。她和她的追随者矛头所向，是阻挡极权主义的防线。 <br />
<br />
　　汉娜・阿伦特对于极权主义起源的分析，既有洞见和精彩之处，也有巨大的盲区和明显的误判。她受海德格尔影响很深，思想深处有贵族式理想化倾向，对英美代议制民主怀有轻视与偏见，她的思想和风格容易在当代引起共鸣、得到欣赏，导致与她的初衷相反的后果。对此，我们应留意才是。</div>

							
							
							
							
