<br />
“Oh My Media！！！”最早起源于人人（校内）网的相册，但是由于人人（校内）网是一个以交友休闲为主题的SNS，并不适合面向公众的内容投放。虽然有分享功能，但是可获知的受众范围仍显狭隘。所以打算将此相册分离，建立独立Blog。将来也不回继续在人人（校内）上更新。<br />
