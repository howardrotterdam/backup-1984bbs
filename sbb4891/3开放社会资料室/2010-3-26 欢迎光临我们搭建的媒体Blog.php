“Oh My Media!!!”这个集体Blog用来记录媒体对于突发事件的报道，以及对异常情况的处理（更需要智慧）。事件发生地点关注于国内，但是媒体的选择是全球。主要是报纸、杂志、网站与电视画面。我们相信任何进步都依赖于一点一滴的积累，绝非一蹴而就。<br />
<br />
内容发布更多来源境内媒体。我们通过Twitter、社会化网络、RSS、BBS、部分报纸杂志的阅读、观看电视等途径获取资讯。<br />
