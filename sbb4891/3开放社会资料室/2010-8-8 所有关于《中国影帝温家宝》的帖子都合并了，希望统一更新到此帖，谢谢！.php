													<h2>余杰：温家宝正面回应《影帝》一书？</h2>
						
						
																											<div id="postmessage_797619" class="t_msgfont">温家宝在深圳调研，强调不仅要推进经济体制改革，还要推进政治体制改革，“停滞和倒退会葬送改革开放的成果”。博讯新闻网在报道该讲话时，评论说：“温家宝在深圳三十年庆典到来之际的喜庆日，反常地大胆批评深圳和广东停滞倒退，可能与香港近日出版了《中国影帝温家宝》一书有关――温家宝借此回应作家余杰：他不是‘影帝’，而是改革家。”<br />
<br />
《中国影帝温家宝》一书在香港上市一个星期以来，成为二零一零年以来最受瞩目和争议的政治评论类书籍。我不知道中南海是否注意到了这本书，是否通过特殊渠道将这本书送到了温家宝的案头。我的写作从来没有将目标瞄准中南海，我是为关心中国命运的普通读者而写作，中南海的态度如何，并不能影响我的写作和我的观点。所以，当秘密警察以“可能危害国家安全”的理由阻止我出版此书的时候，我对这样的恐吓不屑一顾。<br />
<br />
八月六日，针对近期若干地方官员命令警方以“诽谤罪”抓捕记者和网民的事件，最高检察院负责人发表谈话指出：“不能把对个别领导干部的批评、指责乃至过激的言语当做诽谤犯罪来办。”而温家宝这次在深圳的讲话也强调：“要从制度上解决权力过分集中又得不到制约的问题，创造条件让人民批评和监督政府。”正是在此背景下，自从七月五日我遭到北京警方的传讯以来，直至八月十六日《中国影帝温家宝》一书在香港如期出版之后，我再没有受到来自北京警方的骚扰。也许，那个自作主张、邀功请赏的国保官员受到了上级的训斥，不敢再轻举妄动了。<br />
<br />
正如刘晓波在法庭上所作的“我没有敌人”的宣告，我也从未将温家宝和共产党当作我的敌人。所以，我乐于让作为被批评对象的温家宝先生读到此书，也乐于倾听来自温家宝本人的回应。我计划请新世纪出版社的社长鲍朴先生，以联邦快递的方式，邮寄一本《中国影帝温家宝》到总理办公室。至于这本书会不会被海关查扣、会不会被温家宝的秘书拦截，我不得而知。但我希望藉此表达批评者与被批评者沟通与交流的善意。<br />
<br />
比《中国影帝温家宝》这本书本身更重要的，是这本书一波三折的出版过程。这个过程是中国的言论自由的空间艰难拓宽的过程，也是批评者与被批评者不断学习和提升的过程。作为批评者，作为一名独立知识分子，我在此过程中需要学习的是，如何更加客观、中立和理性地对官员及政治体制作批评；作为被批评者，作为政府的“首席公务员”，温家宝先生在此过程中需要学习的则是，如何面对和接纳批评意见，甚至是非常尖锐的、近乎“修理” 的批评意见。正是在这样的学习之中，民主的生活方式才能成为可能。</div>

							
							
							
							
