<br />
1、 个人所得税<br />
　　美国个人所得税是美国在1861年为筹措南北战争的经费而设置开征的一种税。1862年，美国又修改了个人所得税法，使个人所得税具有了累进的性质。1872年到1892年，个人所得税停征。直到1913年，第16次宪法修正案通过了国会有权对所得征税的条款，并通过了修正宪法之后的第一个个人所得税法。从美国1913年所得税法建立以来，又经过了若干次修订改革。总的说来，税率是不断提高的，宽免额也不断增加，所得税在整个财政收入中占据的比重不断上升，从而使美国的税收制度由过去的以间接税为主体转变为以直接税为主体。按照征收级别的不同，美国个人所得税又分为联邦个人所得税、州个人所得税和地方个人所得税，其中，以联邦个人所得税为主。<br />
<br />
　　(1) 纳税人。个人所得税的纳税人为美国公民、居民和非居民。美国公民是指出生在美国的人和加入美国国籍的人。美国居民指非美国公民，但按美国移民法拥有法律认可的永久居住权(如获得绿卡)的人，不符合美国公民和居民身份的为非居民。<br />
　　(2)课税对象、税率。个人所得税的课税对象是美国公民和居民来源于全球的所得，美国非居民来源于境内的所得。<br />
