<br />
(3)应纳税所得额的计算及应纳税额的计算。美国个人所得税的计算步骤是：税法规定的各纳税人的全部所得先减去不予计列所得（为总所得）；然后减去税法规定的应在“调整所得前扣减的项目”（为调整所得）；再减去分项扣减款项或标准扣减款项；再减去免税项目（为应纳税所得额）；乘以适用税率（为应纳税额）；最后减去税收抵免税款和已付税款等，即为最终的纳税款。对每一项目的计算，税法都有详细的规定和计算规则。<br />
<br />
(4) 征收方法。美国个人所得税的应税期限为一全年。特殊情况可少于12个月。全年纳税年度截止于每个日历年12月31日的，为按日历年度计的纳税年度；截止于每个日历年度其他的任何一个日期的，为按财务年度计的纳税年度。<br />
个人所得税的纳税人按日历年度为纳税年度的，在纳税年度次年的4月15日前申报纳税；按财务年度为纳税年度的，应在纳税年度终止后次年的第4个月的第15日前申报纳税；以领取工薪的纳税人，实行由雇主预扣代缴的制度。<br />
