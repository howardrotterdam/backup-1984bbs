继《王实味与〈野百合花〉》、《储安平与「党天下」》之后，历时八年，戴晴完成又一部v史传记<br />
<br />
在如来佛掌中――张东p和他的时代<br />
<br />
一九四九年九月三十日，在中华人民共和国成立前夕举行的中央人民政府主席选举中，五百七十六名投票人，毛泽东得票五百七十五张。半个多世纪后，戴晴推断：投了自己一票的毛泽东，不打算放过那张唯一的反对票，从而引出至今未获平反的「张东p叛国案」。<br />
<br />
张东p(1886C1973)是近代中国不可避的人物。他是最早的西方哲学译介者、著名政论家和报人，也曾在政局刻不容缓之际奔走於多方政要之间，与梁启超、陈独秀、孙中山、蒋介石、毛泽东、周恩来均有直接交往。一九四九年他以国共双方居间人的角色参与北平和谈。毛泽东曾不止一次在正式和非正式场合说，「北平和平解放，张先生第一功」。中共建政后不久，身为第一届中央人民政府委员的张东p，旋被定罪，从此销声匿迹。到了邓小平时代，在几乎所有毛泽东政治运动的受害者获得平反之后，「张东p案」依然是个禁区……<br />
<br />
作者耗时八年，遍访人物，广X史料，精心辨析，完成了这部v史纪实之作（Historical Investigative Journalism）。本书以张东p的一生为主Q，旁涉大批与张东p同时代的人物（除上面提及的政坛人物，还包括张澜、李济深、彭泽湘、章伯钧、费孝通、雷洁琼等），书写了从清末到文革一个又一个风云变幻的时代。作者以特有的v史敏感，触及军事、政治、思潮、文化、社会、人性等多层面的纠葛，展现出中国现代化转换过程中种种艰辛和困惑，为现当代史研究者和关注v史的读者揭开大量不为人知或被刻意遮蔽的v史谜团。<br />
<br />
本书书名取自毛泽东就一件由他与周恩来亲自掌控的「社会名人」要案给公安部的亲笔批示。「如来佛」是毛泽东的自喻。但问题是，主宰他人生死的人是否真的能主宰v史？<br />
「在抓阶级斗争的时期，由公安部某局负责侦控的一个政治案件，通过裼眉际跏侄危查获了被侦察对象的一些可疑活动。因为该对象是个社会名人，需及时向中央报告，又要注意保密。案件的进展情况公安部只报周总理和毛主席。上报材料不打印，由专人手抄一份，公安部常务副部长徐子荣签名后，以绝密件报周总理办公室。几天后，原件退回，上面有毛主席亲笔批示：『在如来佛手掌之中。』」<br />
<br />
（邢俊生：《公安史话．我给徐子荣当秘书》，2007年11月21日）<br />
<br />
戴晴简介： 戴晴，当代著名作家，记者，曾获美国哈佛大学尼曼学人奖学金、世界报业协会自由金笔奖等多项国际荣誉。作品包括《王实味与〈野百合花〉》、《储安平与「党天下」》、《长江长江：三峡工程论争》（主编）、《我的四个父亲》等。<br />
