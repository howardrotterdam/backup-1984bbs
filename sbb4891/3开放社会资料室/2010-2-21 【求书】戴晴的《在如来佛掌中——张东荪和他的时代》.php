<br />
後 记<br />
<br />
如果不是在异国他乡遇见张鹤慈―仅因求知之渴望即遭十数年囚禁的「Ｘ案主础埂传主於我，恐怕只是一个飘散在旧籍陈档中的杳渺魂灵。但东p先生以及他的同们，曾经多N生动、有骨气地生活过―哪里像我们，逃出口号即陷进广告―但槭颤N谁都不敢、不愿（间或也做出不屑）说他？更何况，又有谁有本事在官员当道的中国，调出他的卷宗、追踪案件经手人、遍访他的故旧，呈上一部信史？<br />
<br />
澳洲国立大学的白杰明立刻捉住这中国近现代绝对不可无视的人物。十多年来，只要碰面或通信，一定是：「动笔了N？写到哪儿了？什N时候完成？」他推荐我到大洋彼岸的Woodrow Wilson Centre（著者获一九九九―二ＯＯＯ年研究基金），未能完成；又将我捉至南半球，关在Contemporary China Centre, Australian National University（著者获二ＯＯ七年研究基金），生怕旁\连连的著者在北京浪掷光阴。<br />
<br />
挣扎着熬过艰难岁月的张家后人，打开尘封的宝库：记忆中的；侥逃过劫难之旧箱子里的。已经不再是燕京学生、早期民盟干员、后期民盟副主席的叶V义，也在离世前，出於对传主（以及施虐者）的认识、再认识、再再认识，倾力帮助在中国近乎「路路不通」的著者。<br />
<br />
书稿草成。如果没有中文大学出版社鼓励性接纳，以及只有严格的学术出版机构才肯投入的上乘后期编辑：李大兴、胡泊、黎耀强，以著者资v及所受训练，恐怕只能出手一个记者写的大故事。也要感谢另两本《张东p传》的作者，著者得益於他们对传主著作所下的工夫。<br />
<br />
另有一事，难於⒊萑从植坏貌焕鲜嫡腥希赫偶仪资敉蚰阎中保存下的文字、诗词手E，工科出身、仅描过几天红模子的著者，竟不全认得―查书法字典、叨扰古文家也未尽全功。书内引文中留下之「□」，切望学富五车的读者百忙之中慷慨出手友情辨析。<br />
<br />
著者一生大部分时间都是在欺蒙与箝制下度过的。随着已然敞开的国门和高效的信息互联，前天、昨天和今天被掩盖的桩桩件件将一一大白：它们不仅直接关涉国运，更是成千上万，乃至上十万、上百万、上千万及上亿家庭或生或死（或者生不如死）的直接动因。<br />
<br />
二十多年前，中国诗人北岛呼喊「我不相信」；中国歌者崔健嘶叫「我们一无所有」。本书即将付梓，著者也想说我不相信，不相信中国百姓会永远苟安於欺蒙中。本书即将付梓，曾经一无所有的我们，清楚知道今天之所有，并不能体现完整的人类尊严，拼死也要争回本属於自己的东西：思想独立，言论自由。<br />
<br />
戴 晴<br />
