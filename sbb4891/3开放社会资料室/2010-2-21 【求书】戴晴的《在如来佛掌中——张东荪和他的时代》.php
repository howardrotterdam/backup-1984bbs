开 篇<br />
<br />
投票选举中央人民政府主席<br />
<br />
一九四九年九月三十日。中国人民政治协商会议第一届全体会议最后一天。<br />
<br />
这一名称非常重要。之所以选择这一名称而非其他，暗含了即将诞生的共和国之合法性。因椋如果国内、国外舆论已经认可了国民政府於一九四六年召开「老政协」对改组政府与召开国民代表大会所具有的合法性，那N，把椤腹民党反动派」所破坏的政协会议接着开下去，就有了不言而喻的正当性。筹备期间，参与者多泛称其椤感抡协」。一九四九年九月会议正式举行后，新政协郑重更名橹泄人民政治协商会议。对此，毛泽东毫不隐讳。他在开幕词中说：<br />
<br />
我们的会议之所以称檎治协商会议，是因槿年以前我们曾和蒋介石国民党一道开过一次政治协商会议。那次会议的结果是被蒋介石国民党及其帮疵瞧苹盗恕…1<br />
<br />
按照议程，两项选举将在这一天举行：先以「整个名单」（即「主席团所提名单」）付表决的方法选出政协全国委员会委员180名；然后，全体代表再以无记名联记的方法选举中央人民政府主席、副主席和委员五十六名。<br />
<br />
宋云彬在日记中对此有详尽说明。他提到中央人民政府委员的选举，是依据二十九日全体会议的决定，以无记名联记法投票，代表在选举时有权纹名单中的任何一人或若干人而另选，选票上每一候选人姓名下都留有空白，如代表不愿选某人，可在某人姓名上加一「×」，在下面另写上想要选举的某人姓名；只「×」去某人，不另写他人也可。2<br />
<br />
据《人民日报》第二天报道，这项选举，出任九月三十日大会执行主席及选举总监督的是刘少奇、李立三等五人，周恩来则对选举办法作扼要说明。事实上，面对年龄、经v各异的投票人，榉乐钩鱿址掀保周恩来的说明还真不算「扼要」。比如，他简直像对小学生一样，就选举方法对投票人谆谆叮嘱：「要用钢笔，没有的我们秘书处会送上毛笔。在要选的人名字上打圈，要重重的圈。不要选的打叉，叉要重。折叠 的方法是这样的…… 」他还特别说明代表们可以圈选自己。3 叮嘱之后，刘少奇宣眩骸傅交嵊醒【偃ǖ拇表共五百七十六人。」<br />
<br />
《人民日报》记者对会议作了如下描述：<br />
<br />
……如数发下选票后，在我们开国史中最庄严的仪式正式开始。每一个人经过一度深思，立刻在选票中表达出自己的希望。其实，代表们都是胸有成竹的。谁领导了中国的革命，谁把灾难重重的中国人民解放出来，谁一定被选橹醒胛员会主席。他会继续领导我们，永远走向胜利。<br />
<br />
大会选出六十个代表作监票人。九个票箱由九个监票人监守着。监票人详细检查了票箱，小心谨慎地锁起来，钥匙交给执行主席，然后开始投票。整个过程是那N严肃认真，表现着政协会议从始至终的精神。毛主席仔仔细细写好了自己的票，在四时二十分整，把票投进第三号票箱。<br />
<br />
从开票箱中检出五百七十六票，与发票数目完全相符。执行主席李立三说：「有选举权的代表都投票了，我们的投票是有效的。」人民热烈鼓掌，庆贺投票手续的完美无缺。4<br />
<br />
投票完毕，检票人进行检票时，全体代表没有f着：通过了政协会议宣言、给解放军致敬电、竖立纪念碑决定，还随即到天安门广场行纪念碑奠基礼。然后，回到会场听取选举结果。<br />
<br />
再看《人民日报》的报道：<br />
<br />
七时三十分，执行主席刘少奇宣蜒【俳峁。他一字一句地说：「中央人民政府主席，毛泽东，五百七十五票。」会场代表一致起立，热烈鼓掌。乐队奏起「东方红、太阳升、中国出了个毛泽东」的乐曲。代表们合着乐声的节拍鼓掌，其中并夹着「毛泽东万岁」的口号声。乐声刚刚停止，有节奏的掌声又升扬起来。全场情绪沸腾，欢欣鼓舞。这是望所归，每一个人都樽约和读宋按罅煨湟黄倍感到光荣、骄傲。5<br />
<br />
当时的军队代表、作家刘白羽的叙述尤樯动：<br />
<br />
（从天安门广场）回到怀仁堂，我忽然感觉到今天的灯光格外明亮。今天在这里，一个全新的、独立自由的新中国就要诞生了，所有的代表都和我一样，心里有着一种兴奋和激动。<br />
<br />
七点三十分，执行主席刘少奇按铃、开会，随即宣蚜搜【俳峁，当宣训溃骸该泽东以五百五十七票（读者知道，这数字应槲灏倨呤五。这一错误，不知出於排版工人的疏忽，还是刘作家自己激动得顾不得那许多细节了――著者按）当选橹谢人民共和国中央人民政府主席」时，全体代表都兴奋得站起来，一面鼓掌，一面欢呼：「毛主席万岁！毛主席万岁！」乐队奏起「东方红，太阳升，中国出了个毛泽东……」v史在这里翻开了新的篇章。毛主席巍然站立在人海之中，垂手肃立脸上露出严肃的表情。大家都知道，他接受的不只是全场的鼓掌与欢呼，在这一刻，他承担起了中国人民的命运。……<br />
<br />
至此，大会执行主席宣阎飨团的工作结束，下面请新选出的中央人民政府主席、副主席主持大会闭幕式。毛主席首先走上主席台，全体代表起立热烈鼓掌，而后主席、副主席一起站在主席台上。宋庆龄站在毛主席左面，朱德在毛主席的右面。<br />
<br />
毛主席站了好一阵，等候人们激动的浪潮平息。但浪潮一直没有平息，他终於提高声音宣眩骸钢泄人民政治协商会议第一届大会已经圆满成功，现在请朱德副主席致闭幕词。」<br />
<br />
当朱德走向台口的麦克风，戴起老花镜，宣读闭幕词时，会场才暂时安静下来。他宣读完毕，毛主席站起来正要宣焉⒒崾保一个梦幻般的奇E出现了：庄严的讲台上发生了巨大的变化，他身后的帷幕上，呼啦啦展开了一面巨大的五星红旗。这是经过全体代表决议通过、在中国大地上展开的第一面五星红旗。它是那样庄严、美丽，从那一刻起，它就一直飘扬在天安门广场上空，也同时飘扬在每个人的心里。<br />
<br />
毛主席等代表们就坐之后，刚要开口讲话，暴风雨般的欢呼、鼓掌声又一次升腾而起，达到整个会议的高潮，大家都感觉到，从此刻起，在这一片神圣而光明的土地上，人民终於成了主人。<br />
<br />
毛主席等待着，一任大家把饱满的情感宣泄出来……<br />
<br />
但时间终於到了，毛主席毅然说出了「散会」两个字，随后整个会场的沸腾又一次腾空而起，大家再一次樾碌墓埠凸诞生鼓掌欢呼…… 6<br />
<br />
可能有人注意到，投票人五百七十六名，投毛泽东票的是五百七十五人！那时他们一定认椋这是很自然的呀，那没投的一票当然是毛主席自己的――以伟大领袖的谦虚、自信、豁达，他一定会这N做。半个多世纪之后，我们或许可以认椋对此，起码在当时，在场的人没有过丝毫怀疑。<br />
<br />
但是错了。因椋就在当时，就在那红旗翻滚、乐声嘹亮、热泪挥洒的当场，起码有两个人知道事实并非如此――两个人。一个是确实在自己名字上边郑重地画了圈的毛泽东；另一个人就是没有投他票的那名代表，唯一的一名――本书主人公、时任燕京大学教授：张东p。<br />
<br />
读者会说，别忘了，那是「无记名联记」，谁知道哪张票是谁划的。更何况，就算有一位代表没有在毛的名字上画圈，谁有兴趣追究？你的结论下得未免过於唐突。<br />
<br />
说得不错。可惜属於正常推理。如果我们以此逻辑（而且仅仅以此逻辑）推断毛泽东领导下的共a党的行椋那就失之於简单了。<br />
<br />
著者在此提出一个可能已经有相当多共产党基层骨干知道的故事：<br />
<br />
生於一八八六年的张东p育有四个儿女。长子、北大教授、生物学家张宗炳（一九一四年生）；次子、物理学家、中国科学院学部委员张宗燧（一九一五年生）；三子、毕业於西南联合大学社会学系的张宗猓ㄒ痪哦Ｏ年生）；最小的女儿、高能物理学家张宗烨（一九三三年生），如今是中国科学院院士。<br />
<br />
张宗烨一九五二年考入北大物理系，属於成绩优秀的安静学生。毕业时候填报志愿，因樘爱物理而政治上不大开窍，竟填报了「科学院近代物理所」，也即一九五八年后的原子能研究所。可能因檎馀学生实在是「除了物理，不问其他」，居然皮肉无损地从事高度机密的工作凡五十年。<br />
<br />
大约一九九三、一九九四年，在一次会议或者工作间隙，张宗烨碰到了五十年代后期从中宣部调到该所，除物理外，一直从事党的工作――当时是近代物理所的党支部书记、如今也已是按时交纳党费、一切以党的利益樯系暮戊疋油志。<br />
<br />
在和她聊天的时候，忠於科学忠於党、性情却是天真又直率的何祚庥，突然很感慨地对他的老部下提起当年「老话儿」：「这N些年都没告诉你，当时我们可是大大地保了你。你到所里一直是内控使用。……我们给你说了好多好话。其实对你一直就是一种……。刚刚解放，中央人民政府选毛泽东当主席……结果这里边居然有一张反对票。当时他们就猜，唯一的可能就是你爸爸干的。虽然不能肯定，但他们猜除了他不会有别人。」<br />
<br />
张宗烨没敢追问何同志这内部信息的来源。她估计是反右期间传达下来的。当然她也没敢去想，「他们猜」里边的「他们」究竟是谁、以及到底榱耸颤N、依据哪条规矩，去研究那张反对票。「何祚庥跟我说当时他印象非常非常深：那时刚解放啊，无论如何没有想到会有人投反对票――敌人这N快就钻到这N小的圈子里边来了。」7<br />
<br />
如果读者（特别是v史学界）依旧认檎庖彩綮恫豢删⌒诺摹腹轮ぁ梗我们只好寄希望何祚庥和别的一些榈乘信赖的好同志站出来作证；8如果他们不肯，而普通人对政协与人民政府档案的查询权仍不见任何松动…… 就请读下边的故事吧。<br />
<br />
主人公的故事将告诉你，那一张连投票者自己都知道绝对不会对结果发生任何作用的、在毛的名下划了「X」的票，槭颤N会在这时刻投出去。<br />
<br />
於是，我们将知道，对v史以及活跃其中的人的命运的阐释，有没有板上钉钉的证据，可能并不是最主要的。因橹ぞ莼崾落（甚至会篡改或伪造），但由思维主导着的行椋却会在一个接一个历史时刻，爆发出耀眼的道德与智慧光彩。<br />
<br />
