													<h2>美国华人精英组织--百人会</h2>
						
						
																											<div id="postmessage_371810" class="t_msgfont">百人会<br />
<br />
百人会（Committee of 100）是美国的一个华人精英组织，目前有会员130人左右，全部是在美国社会中有影响力与知名度的华裔组成。该组织由著名美籍华裔建筑师贝聿铭等人发起，成立于1990年，常设机构设于纽约。对促进美国与中国以及两岸之间的政治、经济交流起到了特殊的促进作用。与在美国的其他众多华人社团不同的是，百人会的入会资格十分严格，必须由会员推荐，经其他所有会员审核过关之后才会接纳。<br />
<br />
百人会的宗旨有三个：<br />
<br />
旅美华人能在美国立足生根做贡献； <br />
增进中美邦交； <br />
改善两岸关系。 <br />
该组织是在美国最有影响力的华人组织之一，每年的年会都会邀请美国政、商界的重要人士参加，例如2003年的年会就邀请到了美国副助理国务卿和前《纽约时报》总编辑等人发表演说，阐述中美关系发展。此外，百人会与两岸之间的互动也十分频繁、顺畅，百人会曾多次组团访问中国大陆，并曾与包括前总理李鹏、朱F基等在内的最高层领导人会晤，而每年的年会也都邀请中国驻美大使参加。百人会与台湾方面、特别是泛蓝阵营的接触也很密切，连战、宋楚瑜等也曾接见过百人会的代表团。而1992年两岸历史性的新加坡汪辜会谈，就是由汪道涵委托，通过百人会成员向李登辉等人转达之后促成的。<br />
<br />
百人会著名成员包括了建筑师贝聿铭、前柏克莱加大校长田长霖、美国首位华裔州长骆家辉、流行性疾病研究专家何大一、雅虎网站创办人杨致远、慈善家唐仲英、台积电董事长张忠谋和华人女作家张纯如等人。<br />
<br />
专访美百人会会长：傅履仁<br />
问：百人会从1990年初成立到现在，组织机构是不是已经比较完善？<br />
傅履仁：百人会成立时人很少，只有五六十人，也没有专门的办公室和工作人员。我1993年加入时也就只有70来人。现在情况有了很大改善，会员已增加到142人，总部设在纽约，在华盛顿、旧金山和洛杉矶还设立了分部。不但有了全职的工作人员，而且组织机构也比较完善。 <br />
问：百人会是由建筑大师贝聿铭和大提琴演奏家马友友等人发起成立的，您能否介绍一些其他知名会员？ <br />
傅履仁：百人会的会员中有很多华裔精英人物，除贝聿铭和马友友外，还有前华盛顿州州长骆家辉、流行病专家何大一、雅虎公司创办人杨致远、神探李昌钰和华人社会活动家陈香梅等。此外，文艺界的著名导演吴宇森、作曲家谭盾和演员陈冲等都是会员。<br />
问：加入百人会有什么标准吗？<br />
傅履仁：加入百人会最重要的标准就是：在自己所从事的领域要在全美甚至全世界都有知名度，此外还必须得有好的声望和性格。现在，百人会的声望上去了，很多人想加入。如今年我们在旧金山开年会时，曾当过美国宇航员的焦立中提出想加入百人会。焦立中曾是美国唯一的华人宇航员，曾在太空呆了6个月，还在太空行走过三四次，这样的人就符合在自己的领域具有很高知名度的标准，我们当然欢迎他加入百人会。<br />
加入百人会有严格的标准，不是说随便能加入的。一个人要想加入必须得有3个人推荐，百人会还有一个专门委员会来进行审核和批准。关于百人会的年费，现在每年是2000美元左右，但我准备涨年费，因为现在活动大大增多了。 <br />
问：百人会每年最重要的一个活动就是举行年会，今年的年会是在旧金山举行，主题是“金桥连中美”。您能介绍一下这方面的情况吗？ <br />
傅履仁：今年的年会于4月20―22日在旧金山举行，主题是“金桥连中美”，蕴涵着我们愿意当作一架促使中美关系不断好转和发展的桥梁的意思。会议期间，两位美国参议员范斯坦和弗里曼在会议上发表了主旨演讲，美国前商务代表巴尔舍夫斯基和北京大学国际关系学院院长王辑思等在会议上发言。来自金融、网络和电脑业界的企业家还在会议上探讨了本行业的发展趋势和中国市场。 <br />
问：您觉得华人这些年来在美国社会中的地位有什么变化？<br />
傅履仁：华人在美国社会中取得了很大进步。我1950年刚来美国时华人很少，只有从广东来的一些老华人。到上世纪70年代，约翰逊总统废除了一年只能有50个中国人来美国的法律后，华人才逐渐多了起来。现在，亚洲人占美国总人口的4%―5%。 <br />
来美国的华人越来越多，为美国社会做出的贡献越来越大。另外，中国的国际地位越来越高，华人的地位也就随之提高了。 <br />
问：听说您曾当面告诫台湾领导人陈水扁说，“‘去中国化’是错误的”？<br />
傅履仁：是的。我见过陈水扁五六次。2004年我以“大西洋理事会”董事身份访问台湾期间，我提出要跟陈水扁谈一谈。我告诉陈水扁：台湾也是中国文化，台湾方面修改台湾地图等“去中国化”的做法太幼稚、太没道理了。陈水扁听了以后也没有什么话说。<br />
