以嘲讽的口吻和野史的笔法弄出来的毛泽东传记。<br />
　　比较令我意外的是，对毛并非完全的负面攻击，在嘲讽或者丑化的同时，也说了他的巨大功绩。同样，在表现文革的灾难的同时，也提到了文革十年中，中国在各方面的巨大进步。现在很多人在回顾改革开放30年时，常有意无意地将改革开放前的20年贬为停顿荒废的20年，反而不如这部片子客观。<br />
<br />
<br />
<br />
<i>引用 </i><br />
<br />
今天回家较早，吃方便面的时候无所事事，把前段时间五元买来的《毛的激情》播放一下，谁知一看不可收拾，耗费了差不多两个小时看完。片子诙谐幽默的动画和纪录片相结合的叙述方式让人耳目一新，显然，随着思想的开放和社会的发展，老人家不仅仅会面临“走下神坛”问题，估计有一天会被解构得连灶坛也扶不上去。 <br />
<br />
片子一开始就充满着美国人吊儿郎当且无所畏惧装模作样的幽默感，让人感觉大概是“亡我之心不死”的西方敌人又在丑化我们的伟大领袖，影片以穿插着貌似正经的影像资料、插科打浑的动画和一板一眼的采访来构成毛的一生，尤其是动画专讲韵事和糗事，活脱脱把毛塑造成一个“风流才子加伟人”的美式英雄，正符合以牛仔文化为代表的西方审美观，猜想老外们看了这个片，一定会眼巴巴地望着伟岸的领袖和文化大革命，真诚的喊着“官人我也要”。 <br />
<br />
不过随着叙述的发展，会发现导演显然是毛的粉丝，这部美国人李芾甘（Lee Feigon）主导的电影虽然推翻了毛的神奇，却也偏袒了毛的过失。或者说，戴着美国式自由的浪漫眼光来看待毛和他所代表的一群人为这个国家制造的巨大灾难。不可否认，四十年前发生在这片古老大地上的“男女平等”“文化革命”“民主改革”即使是在今天看起来，仍然是非常前卫和时髦的，这一点无疑令没有经历过这场灾难的西方左派分子充满了向往甚至意淫。但是错误在于，当伟大人物出现在一个不再需要伟大人物的时代，带来的毁灭就会和他的伟大成正比。换句话说，一个不需要伟人的时代才是真正伟大的时代。导演李芾甘（Lee Feigon）先生显然没有设身处地地想象过倘使所有美国人都举着红宝书在他们的国会广场上如痴如狂地喊口号，假以“革命”之名把老祖宗那点可怜的家当砸得稀烂，或者举国上下争相为了维护伟大人物的一点口水而互相检举揭发迫害斗争，我想一定会把费城制宪会议上的55位祖宗气得集体在棺材里打滚。 <br />
<br />
毛主席不仅是伟大的，而且是浪漫和前卫的。但是前卫的思想结合浪漫主义，在一个落后的社会泛滥就会成为灾难。这使我想起邻国印度的另一位伟人甘地，因“罗拉特法案”而起的“非暴力不合作”运动在1921年被打破，他不顾大多数人的反对而中止了这场运动，理由是“我们的人民还没有做好准备”。在一个还没有学会民主的社会，民主难免会演变成一群人推翻另一群人而自己当皇帝的冠冕理由，这一点，“文化革命”的造反精神在数十年后却神奇地消失无踪，伟大的中国人民又进入“做稳了奴隶的时代”，倘使主席在天有知，就应该在纪念堂的水晶棺材里打个滚以示不满。 <br />
<br />
不过不管怎样，比起我们见惯“最伟大、最正确、最光辉、最正经”的“CCTV体”纪录片来说，这部片子至少让主人公恢复人性，忍俊不禁，还是值得一看的，效果至少超过《憨豆先生》，这一点可以向毛主席保证。<br />
