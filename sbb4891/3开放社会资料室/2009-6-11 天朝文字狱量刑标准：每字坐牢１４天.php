　　“文革”中，诞生了许多知青歌曲，在这众多的知青歌曲中，《南京知青之歌》无疑是影响最大、流传最广的一首。1969年9月，它被定为“反动歌曲”，因为它“说出了帝修反想说的话，唱出了帝修反想唱的声音”。1970年2月，张春桥指示：迅速查清作者，予以逮捕。<br />
<br />
　　作者其人<br />
<br />
　　《南京知青之歌》反映的是江苏南京市知青下乡之初万分复杂的心情。是当时社会思潮的最直接的反映，具有广泛的社会心理背景。曲折地流露出对命运的无奈、生命活力的被钳制，以及由此导致的失落、压抑、迷茫。但在其中仍注入了深重的历史责任感。<br />
　　关于《南京知青之歌》的作者，在“文革”中有诸多传说，歌曲作者任毅同他的歌一样有一段曲折的经历。<br />
　　任毅，1947年生，南京五中1966届高中毕业生。从小就兴趣广泛、爱好艺术。他上小学时参加过闻名全国的艺术团体―――南京市小红花艺术团，学习唱歌。中学时又参加过南京市中学生艺术团，学习二胡和吉它。在中学时，因为他是个活跃分子，同学们就唤他“11号”。11号就是纳，化学元素中最活跃的一种。<br />
　　1968年12月26日毛泽东诞辰日，南京市五中下乡知青在这一天，乘坐着汽车穿过新建的南京长江大桥，来到插队落户的地方：江苏省江浦县乡下。任毅等人被分配到公社所在地的一个生产队。<br />
　　在刚下乡时，知青们相信接受贫下中农再教育是必要的，所以知青们劳动都很卖力，特别是任毅，干活肯下死力气，肯拼命，工分在当地农村是最高的。但是，不久，知青们便开始怀疑，知识青年似乎给农村带来了新的负担，而农民也并不欢迎他们。<br />
<br />
　　知青的歌<br />
<br />
　　1969年夏收之际，任毅所在公社的知青中普遍弥漫着一种下乡后的失望情绪。<br />
　　全公社的知青经常跑到任毅的知青点来聚会，知青们情绪都很低沉，感到前途渺茫。当时知青中有不少人在弹吉它。吉它在“文革”中是资产阶级的标志，不少家庭因惧怕抄家，纷纷将吉它卖掉。任毅等人的吉它都是在旧货商店买到的，价钱便宜，质量却不错。1969年夏天，由于普遍的失落感，在知青点弹吉它的人又逐渐多了起来，知青们甚至把这里称为“吉它之乡”。<br />
　　在1969年5月下旬的一个晚上，知青们又挤在任毅的小草房里，搜肠刮肚地把会唱的歌轮番唱了一遍。唱完之后，大家仍然觉得心里空虚，这时，任毅的一个朋友唐又龙忽然站起来对任毅说：“工人有工人的歌，农民有农民的歌，任毅，你就写一首我们知青的歌吧。”<br />
　　他的话对任毅触动极大。当晚任毅抱着吉它谱写了《我的家乡》，整整写了一夜。在第二天天色发亮的时候，这首歌终于完成了。他在歌谱上写下了演唱要求：“深沉、缓慢、思念家乡的。”然后，又注明―――南京市五中集体词曲。<br />
　　任毅的创作从某种意义上讲，是崔健摇滚的前奏曲，无可奈何与悲怆，正是一代早熟青年的共同特征。<br />
<br />
　　广为流传<br />
<br />
　　《我的家乡》一歌作出后当即被人拿去传抄，以惊人的速度在知青中间流传开来。夏收之后，任毅在回南京的轮船上听到有人唱这首歌，于是他走过去，故意问：“你们唱的是什么？”那些人很不屑地回答他：“看你的样子像是知青，怎么连这首歌都不知道，这叫《知青之歌》！”<br />
　　当初他写这首歌时并不敢称《知青之歌》，因为这里既没有豪情壮志，也没有宏大理想，它只是表达了知青的一种思乡情绪，表达了知青作为一种非工、非农、非军、非学的特殊阶层强烈的失落感。这种情绪在当时是绝对不允许公开表现的，只允许歌颂上山下乡，甚至只允许表达欢乐。任毅这首歌之所以受欢迎，正是因为它以一种忧郁的调子表达了人的真实情绪和处境。<br />
　　曾有一批上海知青在去黑龙江的火车上，一路吟唱这首歌。江西有一个县的知青在开会前的唱歌比赛中，居然也唱起了这首歌。当时任毅的一些朋友们学着列宁评价《国际歌》的口气说：“凭着这首《知青之歌》，你可以到处找到朋友，找到吃，找到住。”<br />
<br />
　　大祸降临<br />
<br />
　　1969年8月，一个惊人的消息传来。<br />
　　任毅有个同学叫郑剑峰，因身体有点残疾，驼背，所以免去了“上山下乡”，任毅班上同学只有他留在了南京，于是郑剑峰家自然而然成了知青回城的联络点。郑剑峰手很巧，爱装半导体收音机，一天他正在调试时，忽然听到莫斯科广播电台在播放任毅写的歌，他感到很意外，立刻赶去找任毅偷偷告诉他，并约任毅第二天在同一时间再次收听。<br />
　　第二天下午4点钟，任毅来到郑剑峰家，两人躲在小屋中偷偷将半导体拨到莫斯科广播电台的频率上，果然很快收听到了莫斯科的演唱。苏联把它称为《中国知识青年之歌》，采用男声小合唱，配以乐队伴奏，效果搞得很不错。当时任毅听呆了，这是他第一次听到自己这首歌正规演唱的效果，没想到感染力这么大。同时他也意识到莫斯科广播电台的演唱，其实已经把他置于死地，他有不可脱逃的罪责。<br />
　　任毅越听越怕，感到一场大祸即将降临。<br />
　　任毅的预感很快得到了证实，一个月以后，南京街头的大批判专栏贴满了批判这首歌的文章，它已经被定为反动歌曲，“说出了帝修反想的话，唱帝修想唱的声音。”也许是还没有查明作者，当时只是批判歌曲，还没有批判作者。<br />
　　任毅万分恐惧，差不多成了惊弓之鸟。他立即回到知青点，把所有文字烧毁，包括女朋友写给他的信―――她在另一个地方插队，两人经常书信往来，当时两人都对“上山下乡”产生怀疑，在许多方面有共鸣，只是两人关系还未明确。连同女朋友的书信一同焚毁的，还有一本《圣经》，这是任毅从外婆那里拿来的，《圣经》被抄出来也是要定一个罪名的。<br />
　　1969年10月的一天，他背着一个书包，里面装着洗漱用具，自己走到南京市娃娃桥监狱门口，对接待人员说：“我就是《知青之歌》的作者，你们把我抓起来吧！”<br />
　　那人奇怪地看了看任毅，说：“这里也不是想来就能来的，抓不抓你，有上级指示，现在你先回去。”<br />
　　当1970年到来时，形势更为紧张。他正月十一日那天离开了南京，不愿自己在外婆家被逮走，于是一个人悄悄回到了江浦县农村的知青点。回到知青点4天后，即1970年2月19日（农历正月十五）夜，有人来抓任毅。这是根据张春桥的一个指示：迅速查清此人，予以逮捕。荷枪实弹的军人砸开了知青点的房门……<br />
　　深夜，汽车在南京内桥附近拐了一个弯，忽然放慢速度，悄然驶进了一个大门。士兵们把任毅押下来，在一个铁门前站下。任毅突然醒悟了：这不是“娃娃桥”吗？南京人家喻户晓的“进了娃娃桥，小命就难逃”的话倏地闪现在他的脑海中。<br />
　　任毅被推进一个柜台，经过严格搜身，抽去了里里外外所有裤带、皮带，据说为了防止自杀。<br />
　　“拿去，以后不准喊你的名字，这是你的代号。”看守说着将一块1寸宽2寸长的双层白底黑字的布牌子递给任毅。上面印着正楷的阿拉伯字：3427。<br />
<br />
　　十年徒刑<br />
<br />
　　从2月19日任毅被捕入狱，到8月3日被宣布判刑，在这5个半月的时间里，他天天被提出去审讯，而且经常是在夜里。<strong>审讯人员逐字逐句地审查歌曲的歌词，光一句“生活的脚步深浅在异乡”，他们审了任毅好几天，一定要逼他承认当时写的不是“深浅”而是“深陷”。任毅因为自己确实写的是“深浅”，所以拒不承认。虽然是一字之差，可是此时彼时大不相同。关于“深浅”，任毅还可以自我批判一番，因为知识青年身上还存在着没有得到改造的小资产阶级的不彻底性，没有工农兵那种一往无前的脚踏实地的精神，所以脚步是深深浅浅的；而“深陷”岂不是成了对“上山下乡”的一种诬蔑吗？</strong>虽然是一字之差，却关系到任毅的命运，所以审讯过程气氛十分严肃。<br />
　　为了攻下“深陷”这个堡垒，专案组在任毅面前抛出几十个版本的《知青之歌》，估计是从全国各地搜查来的，有油印的、有打字的、有复写的、有手抄的，各种样式的都有，其中确实有不少都是写的“深陷”，大概在流传中人们觉得这个词更能说明知青的状况吧，不过就是这个词把任毅搞得死去活来。<br />
　　在审讯期间，隔三岔五地将任毅拉出去批斗是家常便饭。对他至少开了30多场批判会。<br />
　　为了肃清任毅在知青中的“流毒”，他们还把他拖到插队的江浦县，一个公社一个公社地批斗。<br />
　　1970年8月3日，任毅又被拖去参加一次全省的公判大会，这次被宣判的人中间也有任毅。当判词读到任毅的时候，他几乎不敢相信自己的耳朵：“……判刑10年……”在此之前，任毅已经做好了最坏的打算，从死刑到无期徒刑都想到了，唯独没有想到会给自己一个10年。<br />
　　从8月3日这一天起，任毅离开了整整禁锢了他165个日夜的娃娃桥监狱囚室，被押到劳改农场去服刑，在那里等待他的将是漫长的3000多个日日夜夜。<br />
<br />
　　平反出狱<br />
<br />
　　1979年2月，任毅被平反出狱。出狱后，司法部门赔给他一把新吉它。他被安排到南京一家丝织厂工作。出狱后不久，他到一个老同学家去玩，这位同学的妹妹―――也就是他现在的妻子，对他和他的经历都发生了兴趣，虽然她比任毅年轻许多，却执意要嫁给他。<br />
　　任毅曾任南京市纺织工业供销公司的业务员，现退休在家，家住南京郊区一片新盖的生活小区里。<br />
　　本文作者　郜合启&nbsp;&nbsp;<br />
