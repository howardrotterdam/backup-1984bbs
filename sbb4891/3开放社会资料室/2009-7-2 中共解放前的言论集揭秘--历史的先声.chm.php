													<h2>1940年代中国共产党对国民党独裁专制的批判</h2>
						
						
																											<div id="postmessage_766803" class="t_msgfont">　　　　　　　　　　----------------------------------------------<br />
　　“结束一党治国才有民主可言。”--------1944年毛泽东与谢伟思等人的谈话<br />
　　<br />
　　“目前推行民主政治，主要关键在于结束一党治国。……因为此问题一日不解决，则国事势必包揽于一党之手；才智之士，无从引进；良好建议，不能实行。因而所谓民主，无论搬出何种花样，只是空有其名而已。” ――《解放日报》1941年10月28日<br />
　　<br />
　　“中国是有缺点，而且有很大的缺点，这种缺点，一言以蔽之，就是缺乏民主。” <br />
　　　　　----------《解放日报》1944年6月13日<br />
　　<br />
　　“二十年来，尤其是最近几年，我们天天见的是只许‘州官放火，不许百姓点灯’。政府所颁布的法令，其是否为人民着想，姑置不论；最使人愤慨的是连这样的法，政府并未遵守。政府天天要人民守法，而政府自己却天天违法。这样的作风，和民主二字相距十万八千里!所以民主云云者是真是假，我们卑之无甚高论，第一步先看政府所发的那些空头民主支票究竟兑现了百分之几?如果已经写在白纸上的黑字尚不能兑现，还有什么话可说?所以在政治协商会议开会以前，我们先要请把那些诺言来兑现，从这一点起码应做的小事上，望政府示人民以大信。” ――《新华日报》1946年2月1日<br />
　　<br />
　　“‘共产党要夺取政权，要建立共产党的一党专政’。这是一种恶意的造谣与诬蔑。共产党反对国民党的一党专政，但并不要建立共产党的一党专政。”　――《刘少奇选集》上卷第172-177页<br />
　　<br />
　　“中国人民为争取民主而努力，所要的自然是真货，不是代用品。把一党专政化一下妆，当作民主的代用品，方法虽然巧妙，然而和人民的愿望相去十万八千里。中国的人民都在睁着眼看：不要拿民主的代用品来欺骗我们啊! ” ――《新华日报》1945年1月28日<br />
　　<br />
　　“他们以为中国实现民主政治，不是今天的事，而是若干年以后的事，他们希望中国人民知识与教育程度提高到欧美资产阶级民主国家那样，再来实现民主政治……正是在民主制度之下更容易教育和训练民众。” ――《新华日报》1939年2月25日<br />
　　<br />
　　“限制自由、镇压人民，完全是日德意法西斯的一脉真传，无论如何贴金绘彩，也没法让吃过自由果实的人士，尝出一点民主的甜味的。”――《新华日报》1944年3月5日<br />
　　<br />
　　“要真正做到出版自由，必须彻底废除现行检查办法。”――《新华日报》1945年6月26日<br />
　　<br />
　　“为了国家利益和革命事业，我们应该贡献出自己的一切。但这必须事先解决两个问题，第一，我们那样牺牲自己是真正为了国家和革命么?第二，我们所有的一切是些什么?……一面说青年‘根本不能谈民主’，一面是叫青年‘必须牺牲个人的自由’，这就是在我们这个国度里对青年所施行的‘标准’的‘民主自由’的教育……那不过是为着要装装门面而已。”　――《新华日报》1945年4月15日<br />
　　<br />
　　“反动者企图以‘共党煽动’，轻轻把‘一二・一’惨案的责任推得一干二净，但是七日的新民报说：‘学生罢课反对内战，当地军警出动镇压……，在这情形中谁是谁非，几乎不待判断’，‘看昆明学潮惨案，受害的却是赤手空拳的学生，他们既无武器，更非军队，而竟受到武力的攻击’；‘这次惨案却证明基本人权无保障……政府当局亟须反省’。” ――《新华日报》1945年12月11日<br />
　　<br />
　　“这件惨案的事理至为清楚，责任也很分明：一般青年学生只不过激于爱国热忱，凭了赤手空拳，起来要求民主反对内战，究有何罪?而国民党反动派竟采取残暴手段，惨加屠戮，并在屠戮之后，为了‘嫁祸’起见，还不惜含血喷人，肆意诬蔑，居心恶毒以至于此，真是史无前例。但是人民是不会受欺骗的，人民是最公正的裁判者，国民党反动派要想一手掩尽天下耳目，徒见其日益心劳力拙而已。”――《新华日报》1945年12月7日<br />
　　<br />
　　“本市消息，内政部公开颁行一种限制人民游行自由的法令，借口是‘恐稍有不慎，足以影响社会秩序与公共安宁’。据中央社讯，其要点如下：负责筹备游行的人员，需于事前将姓名、年龄、职业、住址、游行宗旨、 集会地点、进行日期及时间、经过路线等呈报当地‘治安主管机关’。散发的印刷品和张贴的标语须事先送当地‘治安主管机关’审查。上项法令，已由内政部发致全国各省市地方机关，本市市政府业已接到，且已分令警察局及各区公所‘遵照办理’。有了这个‘法’的根据，今后各地当局更可以随意于事先防止临时禁止一切人民团体之游行。人民游行已无自由可言了。”　――《新华日报》1946年5月13日<br />
　　<br />
　　“立即释放全国政治犯!严惩虐待犯人、毒杀犯人的凶手!未获释放的政治犯应切实保证他们的生命安全，不准再有虐待和私刑拷打犯人的非法行为。” ――《新华日报》1946年2月18日<br />
　　<br />
　　“维持一党专政的政策是建立在制造饥饿和灾荒上的，所以这些救灾的治本办法，只有国民党确定的和各党派一道走上和平、民主的道路时，才能完满解决。”――《新华日报》社论1946年3月30日<br />
　　<br />
　　“现在，官方豢养的论客们更公然地企图恐吓人民，说国民党是希望中国安定的，而共产党却希望天下大乱……中国共产党，不但‘要变不要乱’，而且正是要 ‘以变止乱’……（国民党反动派）也是希望某一种‘安定’的，但那并不是全中国的安定，并不是全中国人民的安定，而仅仅是他们坐在压迫人民的宝座上的‘安定’。他们那个小集团可以统治全国、为所欲为的‘安定’……他们的统治‘安定’了，中国百分之九十五以上的老百姓就更会没有饭吃、没有衣穿、没有事做、没有书读、没有说话的自由、没有走路的自由、没有住家的自由……废止国民党的一党专政! ” ――《新华日报》1946年5月17日社论<br />
　　<br />
　　“而在重庆被打得头破血流的青年学生们的组织与行动也被当局宣布为‘不合法组织……妨害治安’，而加以取缔。反之，那些打人的暴徒，是合法的组织，是有益治安，而应力加保护。这就是合法政府的合法措施。让我们在这个不合法的罪名下继续奋斗，一直到‘人民的宪法’出现的一天吧! ”　――《新华日报》1947年2月22日<br />
　　<br />
　　“昨天报载：慕尼黑在上周未暴动，‘革命精神炽烈’，这是真的民意了，‘纳粹调集坦克出动镇压’。希特勒要有他自己的‘民意’，就叫戈林去说话。真的民意出现了，希特勒就派坦克去说话了。”　――《新华日报》1944年3月15日<br />
　　<br />
　　“法西斯的新闻‘理论家’居然公开无耻地鼓吹‘一个党、一个领袖、一个报纸’的主张。它们对于‘异己’的进步报纸，采取各色各样的限制、吞并和消灭的办法，如检查稿件、任意删削，威胁读者、阻碍推销，派遣特务打入报馆、逐渐攘夺管理权，最后则强迫收买，勒令封闭。” ――《解放日报》1943年9月1日<br />
　　<br />
　　“愚民政策虽然造成了沙漠，却绝难征服民心。”――《解放日报》1942年4月23日<br />
　　<br />
　　　　　　　 1940年代中国共产党呼吁在中国实行自由民主，批判自由民主不符合中国国情论<br />
　　　　　　 -------------------------------------------------------------------------<br />
　　“我们的经验证明,中国人民是了解民主和需要民主的，并不需要什么长期体验、教育或“训政”。中国农民不是傻瓜，他们是聪明的，像别人一样关心自己的权力和利益。”--------1944年毛泽东与谢伟思等人的谈话<br />
　　<br />
　　“他们以为中国实现民主政治，不是今天的事，而是若干年以后的事，他们希望中国人民知识与教育程度提高到欧美资产阶级民主国家那样，再来实现民主政治……正是在民主制度之下更容易教育和训练民众。” ――《新华日报》1939年2月25日<br />
　　<br />
　　“他们说这一套都是外国人的东西，决不适用于中国……原来，科学为求真理，而真理是不分国界的……现在固然再也没有顽固派用国情特殊，来反对科学――自然科学的真理了。只有在社会现象上，顽固派还在用八十年前顽固派用过的方法来反对真理……民主制度比不民主制度更好，这和机器工业比手工业生产更好一样，在外国如此，在中国也如此。……有人说：中国虽然要民主，但中国的民主有点特别，是不给人民以自由的。这种说法的荒谬，也和说太阳历只适用外国、中国人只能用阴历一样。”――《新华日报》1944年5月17日<br />
　　<br />
　　“曾听见有一位乡下老先生说：中国人坐汽车会发晕，这就证明汽车只是外国人的玩意。现在却有些已学会了坐汽车的先生们说：中国人民倘过民主的生活，就会出乱子，所以民主只是使用于外国，不合国情，岂不是同样荒谬么？”<br />
　　　　　　　　　　　　　　　　　　　　　　　　　　 －－－－－《新华日报》1944年5月17日<br />
　　<br />
　　“中国共产党和全国人民一样，完全赞成在中国实行民主的政治原则。”<br />
　　　　　　　　　　　　　　　　　　　　　　　　　　　－－－－－《解放日报》1943年3月14日社论<br />
　　<br />
　　“不能因国民程度不高而拒绝民主，应用民主政治教育人民。”<br />
　　　　　　　　　　　　　　　　　　　 －－－－－《新华日报》1939年2月25日社论《民主政治问题》<br />
　　<br />
　　“科学和民主，已经不是新的口号了，但，半个世纪来，赛先生和德先生在中国，正和我们伟大的民主先行者一样，“依然在沙漠上走来走去”！”<br />
　　　　　　　　　　　　　　　　　　　　　　　　　 －－－－－《新华日报》1942年10月19日社论<br />
　　<br />
　　“这正如前天座谈会主席左舜生先生说的：‘我们不去敦促，自由这一客人是永远不会进我们的门的’”!　――《新华日报》1944年5月16日<br />
　　<br />
　　“我们认为最重要的先决条件有三个：一是保障人民的民主自由；二是开放党禁；三是实行地方自治。人民的自由和权利很多，但目前全国人民最迫切需要的自由，是人身居住的自由，是集会结社的自由，是言论出版的自由。”――《中共党史教学参考资料》<br />
　　<br />
　　“现在是非变不可了!但如何变呢?我们只要看看人家，换句话说我们一切要民主。我们一切制度、政策以及其他种种，都要向着能配合世界转变上去改造。” ――《新华日报》1945年4月8日<br />
　　<br />
　　“曾经有一种看法，以为民主可以等人家给与。以为天下有好心人把民主给人民，于是就有了等待这种‘民主’，正如等待二百万元的头奖一样。但是中外古今的历史都证明了，民主是从人民的争取和斗争中得到的成果，决不是一种可以幸得的礼物。” ――《新华日报》1945年7月3日<br />
　　<br />
　　“民主一日不实现，中国学生的爱国运动却是一天也不会停止的。”――《新华日报》1945年12月9日<br />
　　<br />
　　“单说英美吧。英美是民主国家，这是人人公认的。英美人民有各种民主权利……为了国际的地位，必须从保障基本的民主权利开步走。恐惧是懦夫，疑虑是自私，反对便是倒行。我们再度呼吁：保障人民的基本民主权利。” ――《新华日报》社论1944年2月1日<br />
　　<br />
　　“一个民主国家，主权应该在人民手中，这是天经地义的事；如果一个号称民主的国家，而主权不在人民手中，这决不是正轨，只能算是变态，就不是民主国家……不结束党治，不实行人民普选，如何能实现民主?把人民的权利交给人民!”――《新华日报》1945年9月27日社论<br />
　　<br />
　　“现在，假如我们承认战后的世界是一个不可抗而又不可分的民主的世界，那么要在这个世界里生存，要在这个世界的国际机构里当一个‘优秀分子’，第一就是立刻在实践中尊重‘新闻自由’这种人民的‘不可动摇的权利。’” ――《新华日报》1944年10月9日<br />
　　<br />
　　“国际民主既然与国内民主不可分割，所以要想参加到世界民主国家家庭中去的人们，就无法违反国内民主的原则。” ――《新华日报》1944年1月19日<br />
