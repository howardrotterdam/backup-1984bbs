													<h2>共产党几十年前对中国人民许下的诺言--《历史的先声》节选</h2>
						
						
																											<div id="postmessage_154453" class="t_msgfont">党对政府的领导，在形式上不是直接的管辖。党和政府是两种不同的组织系统，党不能对政府下命令。<br />
<br />
――《董必武选集》第54-55页<br />
<br />
<br />
可见民主和言论自由，实在是分不开的。我们应当把民主国先进的好例，作为我们实现民主的榜样。<br />
<br />
――《新华日报》1944年4月19日<br />
<br />
<br />
国际民主既然与国内民主不可分割，所以要想参加到世界民主国家家庭中去的人们，就无法违反国内民主的原则。<br />
<br />
――《新华日报》1944年1月19日<br />
毛泽东，中国共产党的最高政治家，曾经这样表示出中国人民的希望：“我们并不需要、亦不实行无产阶级专政。我们并不主张集体化，也不反对个人的活动 ――事实上，我们鼓励竞争和私人企业。在互惠的条件下，我们允许并欢迎外国对我们的地区作工商业的投资……我们相信着，并且实行着民主政治”。他说得很对。<br />
<br />
――《新华日报》1945年4月19日<br />
<br />
他们说这一套都是外国人的东西，决不适用于中国……原来，科学为求真理，而真理是不分国界的……现在固然再也没有顽固派用国情特殊，来反对科学――自然科学的真理了。只有在社会现象上，顽固派还在用八十年前顽固派用过的方法来反对真理……民主制度比不民主制度更好，这和机器工业比手工业生产更好一样，在外国如此，在中国也如此。而且也只能有在某国发展起来的民主，却没有只适用于某国的民主。有人说：中国虽然要民主，但中国的民主有点特别，是不给人民以自由的。这种说法的荒谬，也和说太阳历只适用外国、中国人只能用阴历一样。<br />
<br />
――《新华日报》1944年5月17日<br />
<br />
目前推行民主政治，主要关键在于结束一党治国。……因为此问题一日不解决，则国事势必包揽于一党之手；才智之士，无从引进；良好建议，不能实行。因而所谓民主，无论搬出何种花样，只是空有其名而已。<br />
<br />
――《解放日报》1941年10月28日<br />
<br />
<br />
必须真正做到民主动员，必须有民主政府持行并保障一切民主的措施，这真理还不简单明了吗?<br />
<br />
――《新华日报》1945年1月18日<br />
<br />
<br />
英国人民把言论、集会、身体等自由作为民主政治的基础而加以无比重视，从美国方面也同样表现出来。上引赫尔国务卿自称一生为这目标奋斗力争的正是这个东西。“平等”与“自由”为什么被民主国家这样重视，重视到认为没有这就无从谈民主政治呢?这是很简单的。国父孙中山先生曾经说：“提倡人民权利，便是公天下的道理。公天下和家天下的道理是相反的；天下为公，人人的权利都是很平的；到了家天下，人人的权利便有不平，……所以对外族打不平，便要提倡民族主义。对于国内打不平，便要提倡民权主义”。英美民主政治所重视的平等，正是这一含义……假如至今英美仍不准人民有平等的权利，那末怎样能够谈得到民主、怎样能够实现民治呢?说到“自由”也是一样，如果连人民言论、集会、身体的自由都不允许，则民治从何谈起?……英国没有成文宪法，但是英国人民有平等有自由，所以虽没有宪法也是民主国家。由此看来，民主政治的主要标志是人民有自由平等的权利……民主的潮流正在汹涌，现在是民权的时代，人民应有言论、出版、集会、结社和身体的自由是真理，实现民主政治是真理，真理是要胜利的，所以高举民主的大旗奋斗着的世界和中国人民是一定要胜利的。<br />
<br />
――《新华日报》1944年3月30日<br />
<br />
<br />
中国要实行民主政治，必须“取资欧美”，但又要避免欧美民主政治的一些流弊，更驾而上之，这正是中山先生的伟大识见。<br />
<br />
――《新华日报》1942年11月12日<br />
<br />
<br />
民主颂――献给美国的独立纪念日：从年幼的时候起，我们就觉得美国是个特别可亲的国家。我们相信，这该不单因为她没有强占过中国的土地，她也没对中国发动过侵略性的战争；更基本地说，中国人对美国的好感，是发源于从美国国民性中发散出来的民主的风度，博大的心怀……但是，在这一切之前，之上，美国在民主政治上对落后的中国做了一个示范的先驱，教育了中国人学习华盛顿、学习林肯，学习杰弗逊，使我们懂得了建立一个民主自由的中国需要大胆、公正、诚实。<br />
<br />
――《新华日报》1943年7月4日<br />
<br />
<br />
七月四日万岁!民主的美国万岁!中国的独立战争和民主运动万岁!打倒日本帝国主义!<br />
<br />
――《新华日报》1944年7月4日<br />
<br />
<br />
杰斐逊的民主精神孕育了两个世纪以来的美国民主政治，杰斐逊的民主精神也推进和教育了整个人类的历史行进。<br />
<br />
――《新华日报》1945年4月13日<br />
<br />
<br />
不论程度之深浅，美国是始终保有一种传统精神的国家，那传统就是民主。<br />
<br />
――《新华日报》1943年4月15日<br />
<br />
<br />
一个平凡而又不平凡的新闻：据说美国在马绍尔战场协助土人实行民主，让他们自己选举行政官。这是很平凡的事：从民主的美国来说，正应当如此。这也是不平凡的事：从不民主或尚未民主的国家来看，觉得新奇、觉得刺耳、觉得不平凡。<br />
<br />
――《新华日报》1944年10月3日<br />
<br />
<br />
我们尊重并且愿意接受美国朋友善意的批评和建议，正如我们对孤立主义提出批评，应受到尊重一样，这也是从彼此激励互求进步以加强两国人民的合作出发的。我们丝毫也不心存疑惧，认为美国朋友的批评是对中国内政的干涉。<br />
<br />
――《新华日报》1944年3月15日<br />
<br />
<br />
如何使青年的思想和行动能有正当的发展……可分两种，一种是主张思想统制。这就是说，把一定范围以内的思想，灌输给青年，对于这种思想是没有怀疑和选择的余地的。……另一种主张是思想自由。……只有自觉和自愿，才能产生心悦诚服的信仰，和惊天动地的创造活动。一般民众都是如此，青年尤其是这样。如果走相反的道路，则结果都是十分可悲的。有许多事实说明在强迫注入的训练之下，青年感到很大的痛苦……这种办法是必须改正的。我们主张思想应当是自由的。<br />
<br />
――《新华日报》1941年6月2日<br />
<br />
<br />
统制思想，以求安于一尊；箝制言论，以使莫敢予毒，这是中国过去专制时代的愚民政策，这是欧洲中古黑暗时代的现象，这是法西斯主义的办法，这是促使文化的倒退，决不适于今日民主的世界，尤不适于必须力求进步的中国……言论出版的自由，是民主政治的基本要件，没有言论出版的自由便不可能有真正的民主，不民主便不能团结统一，不能争取胜利，不能建国，也不能在战后的世界中享受永久和平的幸福……新闻自由，是民主的标帜；没有新闻自由，便没有真正的民主。反之，民主自由是新闻自由的基础，没有政治的民主而要得到真正的新闻自由，决不可能。<br />
<br />
――《新华日报》1945年3月31日<br />
<br />
<br />
作统治者的喉舌，看起来象自由了，但那自由也只限于豪奴、恶仆应得的“自由”，超出范围就是不行的。也就是说你尽可以有吆喝奴隶――人民大众的自由，但对主子则必需奉命唯谨的，毕恭毕敬，半点也不敢自由。<br />
<br />
――《新华日报》1946年9月1日<br />
<br />
<br />
年青的民主的美国，曾经产生过华盛顿、杰弗逊、林肯、威尔逊，也产生过在这一次世界大战中领导反法西斯战争的民主领袖罗斯福。这些伟大的公民们有一个传统的特点，就是民主，就是为多数的人民争取自由和民主。美国现在是反法西斯战争中联合国四大主要国之一，担负了彻底消灭法西斯、消灭侵略、建立世界永久和平安全的重大责任，从美国的革命历史，从美国人民爱好民主自由的传统精神，从美国人民的真正利益，我们深信美国将继续罗斯福的民主政策，不会忽视世界各处，尤其是中国人民的声音，人民的要求。<br />
<br />
――《新华日报》1945年7月4日<br />
<br />
<br />
要真正做到出版自由，必须彻底废除现行检查办法，<br />
<br />
――《新华日报》1945年6月26日<br />
<br />
<br />
为了国家利益和革命事业，我们应该贡献出自己的一切。但这必须事先解决两个问题，第一，我们那样牺牲自己是真正为了国家和革命么?第二，我们所有的一切是些什么?……一面说青年“根本不能谈民主”，一面是叫青年“必须牺牲个人的自由”，这就是在我们这个国度里对青年所施行的“标准”的“民主自由”的教育……那不过是为着要装装门面而已。<br />
<br />
――《新华日报》1945年4月15日<br />
<br />
<br />
“五四”运动以来三十年的中国史，就是学生爱国运动与人民自主运动密切结合的历史，就是学生运动充作人动的先锋和辅助军的历史。在一代的时间内，中国学生用自己的血、泪和汗写下了中国民族民主运动史上光辉的史页，也是世界革命史上特出的史页。事实证明：中国学生将一本过去传统的爱国精神，继续为自己祖国的独立自主和民主自由而努力，也就是为世界和平而努力。<br />
<br />
――《新华日报》1946年11月17日<br />
<br />
<br />
由于各个国家的历史发展、社会状况等具体条件的不同，他们各自所实行的民主政治，可能在形式和内容上，都存在着多少差异。但无论如何，它们之间有一个基本点是相同的，那就是政权为人民所握有，为人民所运用，而且为着人民的幸福和利益而服务。这样的政权必然尊重和保障人民的自.由权利；使失掉自由权利的人民重新获得自由权利；没有失掉自.由权利的充分享有自.由权利；特别是言论、出版、机会、结社，这些作为实行民主政治的基本条件的人民的最低限度的自由权利，是必须切实而充分地加以保障的。<br />
<br />
――《新华日报》1943年9月15日社论<br />
<br />
<br />
<br />
反动者企图以“共党煽动”，轻轻把“一二・一”惨案的责任推得一干二净，但是七日的新民报说：“学生罢课反对内战，当地军警出动镇压……，在这情形中谁是谁非，几乎不待判断”，“看昆明学潮惨案，受害的却是赤手空拳的学生，他们既无武器，更非军队，而竟受到武力的攻击”；“这次惨案却证明基本人权无保障……政府当局亟须反省”。<br />
<br />
――《新华日报》1945年12月11日<br />
<br />
<br />
这件惨案的事理至为清楚，责任也很分明：一般青年学生只不过激于爱国热忱，凭了赤手空拳，起来要求民主反对内战，究有何罪?而国民党反动派竟采取残暴手段，惨加屠戮，并在屠戮之后，为了“嫁祸”起见，还不惜含血喷人，肆意诬蔑，居心恶毒以至于此，真是史无前例。但是人民是不会受欺骗的，人民是最公正的裁判者，国民党反动派要想一手掩尽天下耳目，徒见其日益心劳力拙而已。<br />
<br />
――《新华日报》1945年12月7日<br />
<br />
<br />
中国青年在现阶段中所从事的运动，应该是争取民族独立，经济平等，和政治民主。为这三大目标而奋斗的人，在历史中就有他的地位。<br />
<br />
――《新华日报》1946年11月17日<br />
<br />
<br />
而民主与不民主的尺度，主要地要看人民的人权、政权、财权及其他自由权利是不是得到切实的保障，不做到这点，根本就谈不到民主……保证一切抗日人民 (地主、资本家、农民、工人等)的人权、政权、财权及言论、出版、集会、结社、信仰、居住、迁移之自由权……中国共产党一向是忠实于它对人民的诺言的，一向是言行一致的，因此它的纲领中的每一条文与每一句语，都是兑现的。我们决不空谈保障人权，而是要尊重人类崇高的感情与向上发展的愿望，<br />
<br />
――《解放日报》1941年5月26日<br />
<br />
<br />
单说英美吧。英美是民主国家。这是人人公认的。英美人民有各种民主权利……为了国际的地位，必须从保障基本的民主权利开步走。恐惧是懦夫，疑虑是自私，反对便是倒行。我们再度呼吁：保障人民的基本民主权利。<br />
<br />
――《新华日报》社论1944年2月1日<br />
<br />
<br />
本市消息内政部公开颁行一种限制人民游行自由的法令，借口是“恐稍有不慎，足以影响社会秩序与公共安宁”。据中央社讯，其要点如下：负责筹备游行的人员，需于事前将姓名、年龄、职业、住址、游行宗旨、集会地点、进行日期及时间经过路线等呈报当地“治安主管机关”。散发的印刷品和张贴的标语须事先送当地 “治安主管机关”审查。上项法令，已由内政部发致全国各省市地方机关，本市市政府业已接到，且已分令警察局及各区公所“遵照办理”。有了这个“法”的根据，今后各地当局更可以随意于事先防止临时禁止一切人民团体之游行。人民游行已无自由可言了。<br />
<br />
――《新华日报》1946年5月13日<br />
<br />
<br />
立即释放全国政治犯!严惩虐待犯人、毒杀犯人的凶手!未获释放的政治犯应切实保证他们的生命安全，不准再有虐待和私刑拷打犯人的非法行为。<br />
<br />
――《新华日报》1946年2月18日<br />
<br />
<br />
维持一党专政的政策是建立在制造饥饿和灾荒上的，所以这些救灾的治本办法，只有国民党确定的和各党派一道走上和平、民主的道路时，才能完满解决。<br />
<br />
――《新华日报》社论1946年3月30日<br />
<br />
<br />
一个民主国家，主权应该在人民手中，这是天经地义的事；如果一个号称民主的国家，而主权不在人民手中，这决不是正轨，只能算是变态，就不是民主国家……不结束党治，不实行人民普选，如何能实现民主?把人民的权利交给人民!<br />
<br />
――《新华日报》1945年9月27日社论<br />
<br />
<br />
而在重庆被打得头破血流的青年学生们的组织与行动也被当局宣布为“不合法组织……妨害治安”，而加以取缔。反之，那些打人的暴徒，是合法的组织，是有益治安，而应力加保护。这就是合法政府的合法措施。让我们在这个不合法的罪名下继续奋斗，一直到“人民的宪法”出现的一天吧!<br />
<br />
――《新华日报》1947年2月22日<br />
<br />
<br />
昨天报载：慕尼黑在上周未暴动，“革命精神炽烈”，这是真的民意了，“纳粹调集坦克出动镇压”。希特勒要有他自己的“民意”，就叫戈林去说话。真的民意出现了，希特勒就派坦克去说话了。<br />
<br />
――《新华日报》1944年3月15日<br />
<br />
<br />
现在，假如我们承认战后的世界是一个不可抗而又不可分的民主的世界，那么要在这个世界里生存，要在这个世界的国际机构里当一个“优秀分子”，第一就是立刻在实践中尊重“新闻自由”这种人民的“不可动摇的权利。”<br />
<br />
――《新华日报》1944年10月9日<br />
<br />
<br />
法西斯的新闻“理论家”居然公开无耻地鼓吹“一个党、一个领袖、一个报纸”的主张。它们对于“异己”的进步报纸，采取各色各样的限制、吞并和消灭的办法，如检查稿件、任意删削，威胁读者、阻碍推销，派遣特务打入报馆、逐渐攘夺管理权，最后则强迫收买，勒令封闭。<br />
<br />
――《解放日报》1943年9月1日<br />
<br />
<br />
