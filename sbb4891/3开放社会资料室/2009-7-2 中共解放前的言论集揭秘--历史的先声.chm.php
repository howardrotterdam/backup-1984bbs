　　　　　　　　　　　　　　　 1940年代中国共产党论自由民主<br />
　　　　　　　　　　　　　　---------------------------------<br />
　　“是要彻底地、充分地、有效地实行普选制，使人民能在实际上，享有‘普通’、‘平等’的选举权、被选举权，则必须如中山先生所说，在选举以前，‘保障各地方团体及人民有选举之自由，有提出议案及宣传、讨论之自由。’也就是‘确定人民有集会、结社、言论、出版的完全自由权。’否则，所谓选举权，仍不过是纸上的权利罢了。”――《新华日报》1944年2月2日<br />
　　<br />
　　“可见民主和言论自由，实在是分不开的。我们应当把民主国先进的好例，作为我们实现民主的榜样。” ――《新华日报》1944年4月19日<br />
　　<br />
　　“作统治者的喉舌，看起来象自由了，但那自由也只限于豪奴、恶仆应得的‘自由’，超出范围就是不行的。也就是说你尽可以有吆喝奴隶――人民大众的自由，但对主子则必需奉命唯谨的，毕恭毕敬，半点也不敢自由。” ――《新华日报》1946年9月1日<br />
　　<br />
　　　　<br />
　　“党对政府的领导，在形式上不是直接的管辖。党和政府是两种不同的组织系统，党不能对政府下命令。”　――《董必武选集》第54-55页<br />
　　<br />
　　“有自由的国家不可侮，没有自由的国家，虽暂时强大，最后还是要失败。”<br />
　　　　　　　　　　　　　　　　　　　　　　　　　 －－－－－－《新华日报》1944年5月16日社论<br />
　　<br />
　　“只有建立在言论出版集会结社的自由与民主选举政府的基础上面，才是有力的政治。”（毛泽东答中外记者团）――《解放日报》1944年6月13日<br />
　　<br />
　　“由于各个国家的历史发展、社会状况等具体条件的不同，他们各自所实行的民主政治，可能在形式和内容上，都存在着多少差异。但无论如何，它们之间有一个基本点是相同的，那就是政权为人民所握有，为人民所运用，而且为着人民的幸福和利益而服务。这样的政权必然尊重和保障人民的自由权利，使失掉自由权利的人民重新获得自由权利，没有失掉自由权利的充分享有自由权利；特别是言论、出版、集会、结社，这些作为实行民主政治的基本条件的人民的最低限度的自由权利，是必须切实而充分地加以保障的。――《新华日报》1943年9月15日社论<br />
　　<br />
　　“毛泽东，中国共产党的最高政治家，曾经这样表示出中国人民的希望：‘我们并不需要、亦不实行无产阶级专政。我们并不主张集体化，也不反对个人的活动――事实上，我们鼓励竞争和私人企业。在互惠的条件下，我们允许并欢迎外国对我们的地区作工商业的投资……我们相信着，并且实行着民主政治’。他说得很对。”――《新华日报》1945年4月19日<br />
　　<br />
　　“只有忠于民主制度，坚决地依靠着民主主义这‘生命的活力’的人，才能够在民主制度下继续存在；反之，害怕民主制度的人就是背离了这伟大的生命的活力，而终于会陷于死亡的绝境。”<br />
　　　　　　　　　　　　　　　　　　　　　　　　　－－－－－－《新华日报》1944年11月15日<br />
　　<br />
　　“一切力量来自人民!一切光荣归于民主!” ――《解放日报》1945年7月2日<br />
　　<br />
　　“必须真正做到民主动员，必须有民主政府执行并保障一切民主的措施，这真理还不简单明了吗? ” ――《新华日报》1945年1月18日<br />
　　<br />
　　“英国人民把言论、集会、身体等自由作为民主政治的基础而加以无比重视，从美国方面也同样表现出来。上引赫尔国务卿自称一生为这目标奋斗力争的正是这个东西。‘平等’与‘自由’为什么被民主国家这样重视，重视到认为没有这就无从谈民主政治呢?这是很简单的。……如果连人民言论、集会、身体的自由都不允许，则民治从何谈起?……英国没有成文宪法，但是英国人民有平等有自由，所以虽没有宪法也是民主国家。由此看来，民主政治的主要标志是人民有自由平等的权利……民主的潮流正在汹涌，现在是民权的时代，人民应有言论、出版、集会、结社和身体的自由是真理，实现民主政治是真理，真理是要胜利的，所以高举民主的大旗奋斗着的世界和中国人民是一定要胜利的。”――《新华日报》1944年3月30日<br />
　　<br />
　　　　<br />
　　<br />
　　“一个平凡而又不平凡的新闻：据说美国在马绍尔战场协助土人实行民主，让他们自己选举行政官。这是很平凡的事：从民主的美国来说，正应当如此。这也是不平凡的事：从不民主或尚未民主的国家来看，觉得新奇、觉得刺耳、觉得不平凡。”――《新华日报》1944年10月3日<br />
　　<br />
　　“我们尊重并且愿意接受美国朋友善意的批评和建议，正如我们对孤立主义提出批评，应受到尊重一样，这也是从彼此激励互求进步以加强两国人民的合作出发的。我们丝毫也不心存疑惧，认为美国朋友的批评是对中国内政的干涉。”――《新华日报》1944年3月15日<br />
　　<br />
　　“如何使青年的思想和行动能有正当的发展……可分两种，一种是主张思想统制。这就是说，把一定范围以内的思想，灌输给青年，对于这种思想是没有怀疑和选择的余地的。……另一种主张是思想自由。……只有自觉和自愿，才能产生心悦诚服的信仰，和惊天动地的创造活动。一般民众都是如此，青年尤其是这样。如果走相反的道路，则结果都是十分可悲的。有许多事实说明在强迫注入的训练之下，青年感到很大的痛苦……这种办法是必须改正的。我们主张思想应当是自由的。”――《新华日报》1941年6月2日<br />
　　<br />
　　“统制思想，以求安于一尊；箝制言论，以使莫敢予毒，这是中国过去专制时代的愚民政策，这是欧洲中古黑暗时代的现象，这是法西斯主义的办法，这是促使文化的倒退，决不适于今日民主的世界，尤不适于必须力求进步的中国……言论出版的自由，是民主政治的基本要件，没有言论出版的自由便不可能有真正的民主，不民主便不能团结统一，不能争取胜利，不能建国，也不能在战后的世界中享受永久和平的幸福……新闻自由，是民主的标帜；没有新闻自由，便没有真正的民主。反之，民主自由是新闻自由的基础，没有政治的民主而要得到真正的新闻自由，决不可能。”―― 《新华日报》1945年3月31日<br />
　　<br />
　　“‘五四’运动以来三十年的中国史，就是学生爱国运动与人民自主运动密切结合的历史，就是学生运动充作人民运动的先锋和辅助军的历史。在一代的时间内，中国学生用自己的血、泪和汗写下了中国民族民主运动史上光辉的史页，也是世界革命史上特出的史页。事实证明：中国学生将一本过去传统的爱国精神，继续为自己祖国的独立自主和民主自由而努力，也就是为世界和平而努力。” ――《新华日报》1946年11月17日<br />
　　<br />
　　“中国青年在现阶段中所从事的运动，应该是争取民族独立，经济平等，和政治民主。为这三大目标而奋斗的人，在历史中就有他的地位。” ――《新华日报》1946年11月17日<br />
　　<br />
　　“而民主与不民主的尺度，主要地要看人民的人权、政权、财权及其他自由权利是不是得到切实的保障，不做到这点，根本就谈不到民主……保证一切抗日人民(地主、资本家、农民、工人等)的人权、政权、财权及言论、出版、集会、结社、信仰、居住、迁移之自由权……中国共产党一向是忠实于它对人民的诺言的，一向是言行一致的，因此它的纲领中的每一条文与每一句语，都是兑现的。我们决不空谈保障人权，而是要尊重人类崇高的感情与向上发展的愿望。” ――《解放日报》1941年5月26日<br />
　　<br />
　　　　　　　　　　　　　　 1940年代中国共产党对美国的赞颂<br />
　　　　　　　　　　　　 ------------------------------------<br />
　　“从年幼的时候起，我们就觉得美国是个特别可亲的国家。我们相信，这该不单因为她没有强占过中国的土地，她也没对中国发动过侵略性的战争；更基本地说，中国人对美国的好感，是发源于从美国国民性中发散出来的民主的风度，博大的心怀……但是，在这一切之前，之上，美国在民主政治上对落后的中国做了一个示范的先驱，教育了中国人学习华盛顿、学习林肯，学习杰弗逊，使我们懂得了建立一个民主自由的中国需要大胆、公正、诚实。” ――《新华日报》1943年7月4日<br />
　　<br />
　　“年青的民主的美国，曾经产生过华盛顿、杰弗逊、林肯、威尔逊，也产生过在这一次世界大战中领导反法西斯战争的民主领袖罗斯福。这些伟大的公民们有一个传统的特点，就是民主，就是为多数的人民争取自由和民主。美国现在是反法西斯战争中联合国四大主要国之一，担负了彻底消灭法西斯、消灭侵略、建立世界永久和平安全的重大责任，从美国的革命历史，从美国人民爱好民主自由的传统精神，从美国人民的真正利益，我们深信美国将继续罗斯福的民主政策，不会忽视世界各处，尤其是中国人民的声音，人民的要求。”――《新华日报》1945年7月4日<br />
　　<br />
　　“不论程度之深浅，美国是始终保有一种传统精神的国家，那传统就是民主。”――《新华日报》1943年4月15日<br />
　　<br />
　　“杰斐逊的民主精神孕育了两个世纪以来的美国民主政治，杰斐逊的民主精神也推进和教育了整个人类的历史行进。” ――《新华日报》1945年4月13日<br />
　　<br />
　　　　“像林肯总统和罗斯福总统那样的民主的政治生活中产生的领袖，是虽在战时也一点不害怕民主制度的巡行的。他们不害怕民主的批评和指责，他们不害怕人民公意的渲泄，他们也不害怕足以影响他们的地位的全民的选举。他们不仅不害怕这些民主制度，而且他们坚决地维护支持这些民主制度。因此他们才被人民选中了。――《新华日报》1944年11月15日<br />
　　<br />
　　“每一个在中国的美国兵都应当成为民主的活广告。……我们并不害怕民主的美国影响，我们欢迎它。”--------1944年毛泽东与美国驻华官员谢伟思（John Service）等人的谈话（原载中共中央党史研究室等编《党史通讯》一九八三年第二十至二十一期）<br />
　　<br />
　　“七月四日万岁!民主的美国万岁!中国的独立战争和民主运动万岁!打倒日本帝国主义! ”　――《新华日报》1944年7月4日</div>

							
							
							
							
