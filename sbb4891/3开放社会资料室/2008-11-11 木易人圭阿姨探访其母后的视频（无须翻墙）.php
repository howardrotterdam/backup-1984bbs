																											<div id="postmessage_3757" class="t_msgfont">文字版：<br />
2008年11月10下午，杨佳姨妈王静荣到北京忆通律师事务所会议室，向李劲松律师和刘晓原律师详细介绍了：<br />
<br />
11月9日上午，自已在北京安康医院时，“杨佳母亲王静梅所说的话”及相关情况详情。 <br />
<br />
1、我们居委会的主任王淑兰（音），星期三（2008年11月5日）下午，她给我打电话，我出去刚回来，她就说找我有事，要上我们家来。我说你别来我们家，我说你什么事啊？我就到你们居委会来吧。<br />
<br />
我到居委会后，她说：乡政府，乡政府武装部的部长江部长给我打电话，说让我找你一下，因为你是我社区的居民，你又是党员，属于我管界的，让我告诉你一声，说你妹妹现在精神上受了一点刺激，在医院里治疗呢。<br />
<br />
说你想不想看，你如果想看的话我可以给你联系。<br />
<br />
后来我说我当然想看了，她说那好吧，她当时就给那个江部长打电话，她说江部长你给我的任务我可完成了，我跟王姐说了，她说她去。<br />
<br />
后来对方可能说了一些话，就把电话挂了，她说江部长说了给你联系，让你等信吧，后来我就说行，这么着我就出来。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;2、然后等到星期五的晚上，她给我打电话了，她说我跟你联系好了，定好了，星期日的早晨9点，在你们的那个大门口，我们不是小区吗，她说在小区大门口你等着，有车送你去，后来她说，你是自己去，还是两口一块去？后来我说一块去吧，她说那行吧，江部长说让我也去。我说行啊，你也去吧，就这么着，说好了。<br />
<br />
<br />
周日早上九点我到小区门口就有一辆车等着了，车上司机和主任在上边，等于是我们4个人，主任、我、还有我爱人，司机，我们4个人，这样车就往前开着走了。 <br />
<br />
后来我看这个这个车往东走，也不跟我说去哪儿，也不告诉我去哪个医院，我就问，当时那天通知我去的时候我就问哪个医院，主任说我也不知道医院，后来等她上车了，我就跟傻子似的坐车上就往前开，后来过了东五环了，我说这是五环吧，我的意思就是说提醒一句要到哪儿？是上哪儿啊？后来车上也没人说话，我爱人就说这已经是五环了，后来车就还往前开着。 <br />
<br />
后来半道司机就问主任咱去哪儿？<br />
<br />
你说，有这事吗？<br />
<br />
主任说，我也不知道去哪儿啊？<br />
<br />
开的是乡里边乡政府的车，后来他就问主任说，咱去哪儿啊？主任说我也不知道去哪儿，后来主任说咱们头知道，后来就一直往前开，后来走的下了机场高速以后，这车就不开了，就停了，后来我们就看见还有人在等着呢，后来等了有几分钟，后来又来了一辆黑色的本田车，这车就绕这个车前面来了，也停那儿不走了，这车可能也不知道是什么地方，也等着呢，后来就一起等。 <br />
<br />
我说，这神秘劲，跟地下党接头似的。那个车也靠边，我们就两个车都靠边等着，等了有六七分钟了，后来从后边又过来一辆黑车，后来那个黑车往前开，这两辆车就跟着走了，那可能就是这辆车带路吧，前面那辆车可能也不知道去哪儿， <br />
<br />
真够逗的，司机都不知道上哪儿，可能就跟他说上机场高速出口等着，肯定是跟人这么说的，后来就跟着往前走。<br />
<br />
后来一直往前开，我都不知道是哪儿了，我就知道是顺义，然后就拐一个大门里头，这个大门立着一个大牌子，上面写着北京市公安局强制治疗管理处，那么一个牌子，开进去之后我们就下来了。 <br />
<br />
总共有三辆车，我们前面那辆车下来一个男的，挺高的，还有一个男的瘦的，那辆车下来几个年轻人，还拿着一个摄像的那个。后来那个年轻的人就介绍说，这是主任，都没说我们是谁。<br />
<br />
后来还有一个男的说，这是我们大夫，一个男的，后来拿摄像的人就说你跟大夫说说带她们进去吧，这么着，我们三个人跟着大夫就进去了，后来我就问主任，我说哪个是江部长啊？主任说就那个穿黑衣服的人，挺高的那个。都没敢介绍给我们，都没敢介绍，说我是谁谁谁，怎么着的，都没介绍，完了以后就给我们领进去了。<br />
<br />
 3、进去以后就到医生的办公室，是他们主任的办公室，介绍了一下情况。<br />
<br />
我说，怎么样？<br />
<br />
我以为是神志特别不清楚了，我说，人怎么样了，是不是神志不清楚啊？<br />
<br />
<br />
她说，没有你说的那个严重，她就是情绪上特别不稳定，一会儿闹，一会儿哭什么的，情绪不稳定。<br />
<br />
再一个，她还是人格障碍，她说，人格障碍这个也是我们精神方面的一种疾病，后来她说，这种疾病，你们在外行来看的话，就是脾气不好似的，实际上，她说，这是一种病，后来她说，情绪方面，我们可以给点药控制，但是，人格障碍这方面，就像人秉性难移似的，这种东西不好改，后来就介绍情况。<br />
<br />
<br />
后来我爱人问她说什么时候给送来的啊？<br />
<br />
她说7月2号晚。<br />
<br />
就问她说是哪儿给送来的啊？是派出所吗？<br />
<br />
那个主任说应该吧。<br />
<br />
<br />
后来，这么着，她说，我去叫她吧，她说，你看是在病房看她，还是给她叫到办公室啊？<br />
<br />
<br />
后来我说你们看吧，你们看怎么好，她们说要么给叫办公室吧，病房太乱了，我说那也行吧，主任就叫去了。 <br />
<br />
4、一会儿，王静梅来了，我看跟原来一样，没什么区别，进来以后，王静梅就嚷嚷：<br />
<br />
<br />
我就跟你说吧，你看她们是什么事？ <br />
<br />
把我放这儿好几个月，我什么都干不了，我儿子这么大的事，我不出面谁出面？我最了解他。 <br />
<br />
把我关在这儿，这几个月，给我关一个小黑屋里头，也不给我椅子。<br />
<br />
<br />
后来我说，这是怎么回事，大夫当时也没说话。<br />
<br />
 5、王静梅说：<br />
<br />
我要求出去，也不让我出去，软禁我，这是怎么回事！后来，她就拉着我说，我不行了，我必须得出去，我必须得办点事。 <br />
<br />
后来，她就把这经过说了。<br />
<br />
她说：<br />
<br />
上海民警后来来了两次北京想私了，第一次我没见着，第一次是杨佳跟他们接触的，杨佳说处理这件事有没有回执？他们说没有，后来没拿到协议他们就回去了。<br />
<br />
第二次他们又来了，来了以后我直接跟他们说的，我要求给回执，问怎么处理这事，有一个结果。他们说那没有回执，后来他们说，那你说要怎么着，一万块钱行不行？<br />
<br />
他们提出来的，说一万块钱行不行是民警说的，民警说一万块钱行不行。他们提出的说一万块钱行不行。<br />
<br />
我说你不用跟我说这个，你就给我回执，我们不是为了钱，你给我回执，这么着等于没拿着协议他们又走了。<br />
<br />
我和上海民警的谈话有MP3录音，但这MP3录音现在警察手里，被警察拿走了。<br />
<br />
杨佳最后一次去上海，我都不知道，我上单位去了，回来以后他就走了，我都不知道他上哪儿了，他就走了，<br />
<br />
等到警察找我来了，才说这事，后来警察把家里翻的乱七八糟。 <br />
<br />
她说：<br />
<br />
我还没完呢，我出去以后也没完，你有搜查证吗？就翻我的家，我有什么罪？你这么翻我？ <br />
<br />
后来我说：<br />
<br />
你那个存折在哪儿呢？你回过家吗？ <br />
<br />
她说：没回过。<br />
<br />
<br />
我说：你的钥匙在哪儿呢？在你这儿吗？<br />
<br />
<br />
她说：没有，在派出所呢，派出所给我拿走了。 <br />
<br />
6、我们见她之前，我还问大夫，我说谁给送到这儿来的？我爱人也问她，说谁给送这儿来的？是派出所吗？她们说，应该是吧。<br />
<br />
<br />
后来她就嚷嚷这个：<br />
<br />
深更半夜的给我送到这儿来，弄一个车就给我拉这儿来了，什么事这叫，给我强制弄这儿来了。她说是从派出所把她带到这个医院的。<br />
<br />
<br />
她说：<br />
<br />
你看给我骗这儿，我也没想到这么长时间啊，我家里冰箱什么东西不都坏了？<br />
<br />
我临出来的时候还买了一条鱼准备做呢，你说我出来了鱼还不臭成鱼干？<br />
<br />
什么都没做，全部都放坏了，这叫什么事啊。 <br />
<br />
她说：<br />
<br />
把我打成精神病，幸亏我这人有主见，我明白。<br />
<br />
你给我打什么针，我都不打。<br />
<br />
<br />
她说：<br />
<br />
你想给我打成精神病，不可能的事。<br />
<br />
<br />
她说：<br />
<br />
我自己掌握着呢，谁敢把我怎么着！ <br />
<br />
7、那个大夫就说，说她这个病，不是现在的病，以前就是这样。但是她以前，你们不懂，老以为她是脾气不好，实际上，这是一种精神上的疾病。 <br />
<br />
8、我那个有精神病的叔叔，是我小的时候发的精神病，还是我小的时候，我那时候刚懂事，他在上班，要坐火车，带着工资回家，在火车上所有的钱被人偷了，你想那时候，那么多钱被人偷了以后，他一下受刺激了，这样就有点精神不正常了。<br />
<br />
大概是六几年吧，还是年轻的时候。 <br />
<br />
9、我是这么对大夫说的，我说，我可以随时来看她吧。我说，我将来可以来给她送点东西，送点吃的？大夫说，都可以，她说你来之前给我打个电话就行。<br />
<br />
那个大夫姓潘。她那个电话号码是89474179，潘主任，是女的，主治大夫是李大夫，也是女的。<br />
<br />
这地方全名叫北京市公安局强制治疗管理处。<br />
<br />
那个安康医院，是在大门里边，进了这个管理处以后，再往里走，有一个院，有一个牌子，写着北京安康医院。病人住在安康医院里，那里边都是精神病人。 <br />
<br />
10、后来完了以后，我就出来了，出来以后司机又把我们送到家。<br />
<br />
这个主任跟我们家离的不远，同时下的车，那个主任半道接了一个电话，说，行，我知道了，就挂了，挂了以后跟我说，刚才是江部长来的电话，他说让我跟你们说一声，这个事先别说出去，没什么好处。<br />
<br />
 11、我说：上海有一个姓谢的律师找你了吗？<br />
<br />
她说：找了，来了以后就逼着我签字，还没等我怎么说呢，就要我签字，待会儿就走了。<br />
<br />
是我问她来着。我说有一个姓谢的律师找你吗上海？她说有，来了这里以后就逼着我签字。<br />
<br />
他来了就说这个，他说，杨佳，也挺可怜的，我给他买了两件衣裳，先给我说这个，然后就逼着我签字。<br />
<br />
把我关在这出不去，我也没有办法出去请律师，当时又那么多大夫都在旁边等着，也耽误人家的时间，耽误人家的事，就算了，后来怕，儿子到时候真是没有人辩护也麻烦，就签了。 <br />
<br />
12、一审判决没有告诉王静梅，二审告诉她了，二审是10月31号，上海两个人去的，给她的判决书，给她了。上海给她送判决书是31号去的。她说去了两个人。 <br />
<br />
我记得王静梅跟我在办公室谈了有二十多分钟吧，后来，她说关一个小屋里面，我说，这么着吧，上你屋里看看去，后来大夫说，你去看看去吧，我就到那个屋里去了。 <br />
<br />
到屋子以后，在一块的还有别的人，我就跟她在屋里头，后来那个主任不走，就坐那儿了，后来我说，能让我们两个人单独坐会儿吗？她说好，就出去了。<br />
<br />
之后，王静梅对我说：<br />
<br />
我得出去，我得把事给办了。<br />
<br />
<br />
她说：<br />
<br />
别看我在这里头，我来回溜达。<br />
<br />
我说：<br />
<br />
你放心，杨佳在看守所没人欺负他。<br />
<br />
因为我怕她着急。<br />
<br />
她说，我知道。<br />
<br />
她说这里面有一个人还跟她说，说能见到杨佳，你有什么话，我可以给你带到。<br />
<br />
后来她说：<br />
<br />
你就告诉我儿子，在里面好好锻炼身体，别的我没说的，在里头待一天，就得好好锻炼一天。<br />
<br />
后来，五分钟左右吧，主任就进来了，说，怎么样，差不多了吧。我说，行了，我就先走吧。<br />
<br />
我说，你一个人在屋子也好啊，省得睡觉有人干扰，这里清静。 <br />
<br />
出来以后，主任就说：<br />
<br />
为什么不给她放椅子啊，因为，她自己说了，我儿子要是没有了，我也不活了，我还找一个垫背的，说要给她搁椅子，她拿起椅子把护士给打了，就麻烦了。<br />
<br />
就这一个月她稳定点了，给她一把椅子吃饭用。之前确实有一段时间没给她椅子。<br />
<br />
主要是担心，她自己说她儿子死了，她也不活了，她死还找一个垫背的。她们一听就害怕了，就不给她椅子了。 <br />
<br />
我说，我拿点东西是不是可以给她送来？主任说，行，你们随时可以来，你来给我打电话就行，我说那我记你一个电话吧，我就记了一个电话，我把我的电话也给她了，我说有什么事你可以告诉我，她说行。就这么着我就走了。 <br />
<br />
13、最后分手的时候，王静梅跟我说，我这儿没事，放心吧，我这没事。<br />
<br />
她那个也不是说关着，反正就是病房，她自己一个单独的不到十平米的小病房。病房里面就是一个床，连椅子都没有，有一张桌子，有一个长桌子，然后就什么都没有了。被子这些这些都有。<br />
<br />
 14、还有，王静梅说：来了以后还给我用假名！<br />
<br />
叫刘亚玲，我说怎么叫我刘亚玲啊，那是一个假名，后来用假名用了两个多礼拜，才给我改过来，还给我用假名！<br />
<br />
 15、王静梅今天上午打电话来找我了。<br />
<br />
她昨天还跟我说这个，昨天她说：<br />
<br />
我必须得出去，我得请律师，她说，我得把这事说清楚了，我要请律师，我要把这个事的过程说清楚，我现在最要紧的，就是把这个事说清楚。 <br />
<br />
今天上午，王静梅就是用医生办公室这个89474179的电话，给我打来了一个电话。<br />
<br />
她说，我必须得出去。<br />
<br />
为什么，因为，杨佳的事情我是最清楚的，我是唯一了解他的人。<br />
<br />
我必须得替我儿子说话，我说了，我把我的责任尽到了，他们爱怎么判那是他们的问题了，但是我必须尽力。<br />
<br />
<br />
后来我说，行，到时我再给你打过去吧。<br />
<br />
她说，你打这个电话，办公室经常没人。我告诉你病房的电话。<br />
<br />
后来她就问旁边的人这个电话是多少？那个人死活不告诉她，她就跟人急了，后来急了人家也没告诉她，后来我说这么着吧，你别问了，我就打这个电话，我能找到你，你别再跟人嚷嚷了，这么着，就把电话挂了。<br />
<br />
<br />
她说：<br />
<br />
我必须得出去。<br />
<br />
我说：<br />
<br />
人家不让你出去怎么办？<br />
<br />
她说：<br />
<br />
人家答应我了，要给我办手续。<br />
<br />
我说：<br />
<br />
