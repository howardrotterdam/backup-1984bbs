													<h2>毛泽东的命案？──陈小雅推出新著《中国废片》</h2>
						
						
																											<div id="postmessage_505501" class="t_msgfont">毛泽东的命案？──陈小雅推出新著《中国废片》<br />
<br />
作者：黄琨<br />
<br />
毛 泽 东“第一桶金案”有一个证人，但他却消失在“红色的起点”上，活不见人，死不见尸。他究竟是一个“嫉恶如仇”的人？一个“情圣”？还是一个“疯子”？他的失踪或死亡，究竟是一桩“情案”，还是“贿赂案”？抑或是“构陷案”？<br />
<br />
　　中国大陆历史学者陈小 雅一口气提出如此几个耸人听闻的问题，引出了新着《中国“废 片”──毛 泽 东的命案》(Uncovering an abandoned piece of early history- Mao as a murder)剥茧抽丝的深入分析。这本书由明镜出版社出版，与她2005年推出的《中国“牛仔”》（明镜出版社）和《中国“丈夫”》（香港共和出版社），构成关于解构毛 泽 东的三部曲系列。<br />
<br />
　　这是一部十分认真的历史专著──一提历史专著，读者往往会想到硬皮烫金的精装讲义，或者字里行间散发霉味的“故纸”汇集，当然，最近我们也见到很多那些升格为“电视明星学者”美其名曰“历史普及”、亦正亦野的演讲。但这本书绝不是上述这几类，它关注、探究的是一个十分严肃甚至敏感的话题，而写法上，正如作者自己承认的，是“我试图运用从侦探小说中获得的知识，所作的一次写作尝试”。这次尝试堪称成功──作者设置了若干悬念，谜中套谜，谜外串谜，推理环环相扣，剖析步步深入，忽而穷追不舍，忽而“节外生枝”。读者就像跟随一位侦探，忽而在迷魂阵中苦思冥想，忽而史料库中去伪存真，引人入胜，让读者欲罢不能。<br />
<br />
　　书名的所谓“废片”，意即在电影拍摄过程中作废了的镜头，作者用来比喻那些官方“正史”所舍弃不用的史料。任何一部恢弘的史诗大片，都会有被“导演”舍弃的大量废片，虽然浩如烟海，却深藏尘封，无人问津，更不用说，其中有的还被“导演”出于各种理由而封锁甚至销毁呢。<br />
<br />
　　陈小雅爬梳“正史”遗弃的镜头，从大量零碎模糊的“废片”中钩沉抉微，找到一个又一个肉眼几乎看不见的疑点，然后将蛛丝马迹比照、联系，顺藤摸瓜，拼复成历史“原版”──从毛 泽东与杨开慧父女的关系、新民学会解体，到湖南“自治运动”右转；从远在巴黎的“二八学潮”，到第一次国共合作的失败……正如作者所说，“就像亚马逊河上一只蝴蝶□动翅膀，影响到地球另一边的金融风暴、皇冠落地”，上述这些看来风马牛不相及的人和事，都有一个共同的源头，这就是毛 泽 东“第一桶金案”。<br />
<br />
　　毛 泽东“第一桶金案”是陈小雅在《中国“牛仔”》首次揭密的一桩公案，其缘起，就是章含之在《跨过厚厚的大红门》中讲述的毛 泽东所承认的对章士钊“欠债”：1963年初，“毛主席告诉我（章 含之），1920年，毛主席为筹备党的成立、湖南的革命运动以及一部分同志去欧洲勤工俭学，急需一笔数量较大的银款。主席去上海找到父亲，当然没有告诉父亲要成立共产党，只说是为一批有志青年筹款去欧洲勤工俭学，请父亲帮忙。”章士钊筹集了两万银元，交给了毛 泽东。毛对章含之说：其中“一部分钱确实供一批同志去欧洲；另一部分我们回湖南用去造反闹革命了”。于是自1963年，毛 主席对章士钊还这笔欠了几十年的债，每年派秘书送上两千元。<br />
<br />
　　两万银元在1920年可称得上一笔巨款。陈小雅在《中国“牛仔”》中证明，毛泽东并没有用在资助青年去欧洲勤工俭学上，而是挪为己用，成为他在中共政坛崛起的“第一桶金”，并讨论了他的心态、挪用之举对他本人以及对中国革命的深刻影响。<br />
<br />
　　《中国“牛仔”》出版后，毛 泽 东的“第一桶金案”引起广泛争论。陈小雅听到一种议论：“毛 泽东挪用留法勤工俭学捐款，即使是事实，但你能说他是为自己吗？”而她也正好接触到许多史料，从中发现更多更杂的线索，深入到毛 泽东攫取“第一桶金”之后的行踪与用度，便写作了这本书，来专门探讨“第一桶金案”到底为谁，以及背后深不可测的内幕。<br />
<br />
　　陈小雅从“废片”中发现历史“证词”种种悬疑：毛泽东究竟“得金”多少？章士钊与毛 泽东金钱交割中的在场证人是谁？捐钱的“金主”为何出现二人？捐钱的时间为何出现三种“记忆”？她用各种史料证明，毛 泽东拿到这笔两万银元──后来由真正的捐金者又追加了一万，实际上是三万银元，并没有用在资助青年赴欧勤工俭学项目上，也没有用在建党和在湖南的各种革命团体的活动上。<br />
<br />
　　陈小雅的目光聚焦到一个失踪在“红色起点”的人物──彭 璜。他与毛 泽东早期革命活动关系密切，却在一次与毛 泽 东难得的“推诚相见”后，从历史上消失了，活不见人，死不见尸。<br />
<br />
　　当时湖南革命团体圈内，对彭 璜的下落有各种说法：疯焉？死焉？隐焉？陈小雅进一步分析毛 泽东与彭璜矛盾的性质。她从毛晚年对《云中落绣鞋》的评论入手探索“谜底”，重新揣摩毛彭“双凤求凰”，毛未曾如愿而后转而与杨开慧同居的过程，断言彭的结局肯定与毛 泽 东有关。陈小雅在搜集了彭璜的书信文章，研究了他的思想后，运用了排除式推理：一个反对自杀的人如果死亡，要么死于意外，要么死于谋杀；而一个“疯子”如果自杀，是不会隐藏自己的尸体的。而正是在彭 璜失踪后，毛 泽东神不守舍，就是在中共一大上，也显得心不在焉；他精心经营的新民学会匆匆解体；他担任记录的中共“一大”史料变得残缺不全。<br />
<br />
　　不仅于此，陈小雅还从另外一条思路，去深掘“第一桶金案”的本质。在此事之前两年半，毛 泽东为搞钱接受蔡和森做“恶人”的思想，鼓动留法勤工俭学的“人灾战略”，企图迫使政府赈济，引出杨开慧的父亲在经济责任压力下出具介绍信，力荐毛、蔡去向章士钊取款。而这初出茅庐的第一次得手，使毛 泽 东走上“为达目的，可以不择手段”的不归路。<br />
<br />
　　这一笔钱，并非像章含之所说，是章士钊向社会各界筹集的，实际上是湖南省督军谭延□给毛 泽东等人的钱，陈小雅将之命名为“政治贿金”，实际上毋宁称为“政治酬金”，当然也是一笔收买费。毛与谭投桃报李，礼尚往来，在湖南“驱张运动”中，毛泽 东在舆论上大力配合，为谭延□主政铺平道路；谭延□便借章之手让毛发了这一笔“意外之财”；随后，毛 泽东放弃“废督军”和“人民制宪”的初衷，宣布谭延□政府为“革命政府”，将“湖南改造运动”拨向右转。<br />
<br />
　　由于这一笔资助欧洲勤工俭学的巨款并未到位，造成许多后果，造成了一桩“构陷案”：“勤工俭学捐款被贪污”的谣言使得萧子升百口莫辩，代毛泽东受过。而且巴黎勤工俭学学生处境恶化，酿成“二八风潮”，枪击驻法公使……陈小雅深入分析了当时湖南《大公报》匿名连载的文章，详述湖南留法勤工俭学运动及其筹款和巴黎“二八风潮”内幕，为揭开毛 泽东的“金案”悬疑提出了一个关键的砝码。本书作者还论述了“第一桶金案”的广泛而深远的影响：不仅毛 泽东这次的“统战”经验，成为他后来支持“第一次国共合作”的内在动力；更重要的是内心留下的道德阴影，迫使毛后来向左、再向左，“凡事左三分”，断送了 “大革命”的成果。<br />
<br />
　　《中国“废片”》还讨论了毛 泽 东《祭母文》的真伪，分析他为什么会被人塑造成孝子。此外，作为附录之一，此书还收入了陈小雅接受多维记者高伐林采访时长达数万言的书面回答，广泛地议论了围绕毛 泽 东研究的各种层面的话题。<br />
<br />
　　1955年生于湖南长沙的陈小雅，毕业于湖南师范大学历史系，当过工人、干部、编辑记者，是原中国社会科学院政治学研究所的副研究员，因写作、出版《天安门之变──八九民运史》不见容于当局而被该所除名，成为一名自由学者。她还着有《佛之血──八九\六四研究文集》、《地下的热泉──过渡文化的十年（选章）》、《摇篮与墓地──严复的思想和道路》、《汉尼拔》等；主编出版《沉重的回首──1989天安门运动十五周年纪念文集》、《二十世纪中国学术要籍大辞典．政治学》等书。近年来她潜心研究毛泽东，出版《中国“牛仔”──毛泽东的“公案”及行为、心理分析》、《中国“丈夫”──毛泽东的情事》，为毛泽东研究别开生面，引人瞩目。<br />
<br />
　　陈小雅很自信地说：从长达数万言的这部重构的历史中，你将发现，一个意念的闪动怎样主宰着人的行为；一个人如何影响一个时代的历史，乃至一个国家的命运。<br />
