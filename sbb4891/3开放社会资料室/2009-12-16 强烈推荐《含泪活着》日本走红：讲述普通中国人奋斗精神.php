2009年12月16日 16:01　来源：中广网　<br />
 <br />
　　据中国之声《全球华语广播网》12时48分报道，今天的资讯航班，我们一同飞往日本。今天要我们去日本看一场电影，这部影片并不是什么大制作的重磅影片，没有巨星加盟、没有华丽场景、更没有特技打造。 <br />
<br />
　　主角是一名来自中国上海的平凡小人物，但就是这位普通中国人的奋斗精神感动了整个日本，赚足了大家的眼泪，影片的名字叫做-《含泪活着》。<br />
<br />
　　就像赛跑一样，接力棒我已经拿了，跑了那么多年，我的目标是把这个接力棒最后交给女儿。丁尚彪，这部纪录片的电影主角，他在35岁时告别妻子和女儿，满怀崇敬举债赴日留学，后来为生存所迫，丁尚彪从北海道辗转跑到东京，成为一名非法滞留者，长达十几年黑在日本，为了还债，供女儿去美国读书，丁尚彪不得不从事繁重的体力劳动，并且忍受一家人长期分离的痛苦。<br />
<br />
　　其实这部纪录片是华人导演张丽玲《我的留学生活系列纪录片》的收山之作，这部作品凝聚了张丽玲和摄制组10年的心血，他们奔走于日本、中国、美国，记录了丁尚彪一家三口天各一方的生活。就像张丽玲导演所说的，他们只是想把来自生活的感动传递给更多的人。&nbsp;&nbsp;<br />
 <br />
<br />
　　我们在拍摄过程中，从他们的身上得到了很多启示、感动，得到很多勇气核力量。因为真实的东西它的力量是很强大的，这种力量既然能传达给我们，我想我们是不是也能传达给别人。<br />
<br />
　　该片3年前曾在日本富士电视台播出，一举创系当时全日本最高收视记录，2009年一名日本大学生出于对该片的喜爱，独自游说电影公司投资3千万日元把这部已经尘封三年的电视纪录片又带进了电影院。<br />
<br />
　　那位大学生说，这么好的作品不应该埋没，应该让更多的人看到，从11月底上映至今，这部名为《含泪活着》的电影，几乎场场爆满，由于观众数量大大超过预计人数，该片由原来的中型放映厅，转到大型放映厅，上映场次和时间更是一加再加。<br />
<br />
　　在东京新宿区的一家电影院《含泪活着》的影片海报旁贴满了日本观众的观后感，有人说这部影片给在经济低迷的冷风中前行的日本人带来了温暖和希望，而更让人欣慰的是，今天影片的主人公丁尚彪终于在美国和家人团聚，告别了那段含泪的留日生活。 <br />
