																											<div id="postmessage_199444" class="t_msgfont"><a href="http://tibet.news.cn/gdbb/2009-07/31/content_17257006.htm" target="_blank">http://tibet.news.cn/gdbb/2009-07/31/content_17257006.htm</a><br />
<strong>《西藏一年》：给世界一个真实的西藏</strong><br />
<br />
<img src="http://tibet.news.cn/gdbb/2009-07/31/xin_393070631081082827421.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
&nbsp; &nbsp; 纪录片《西藏一年》于日前黄金时间登陆央视一套,之后央视四套以及凤凰卫视也会相继播出。<br />
<br />
&nbsp; &nbsp; 面对该片制片人、导演书云,才知道这样一位经常奔波于世界各地的女士并不如想象中的强健,反而有些纤弱。书云说,《西藏一年》的拍摄从申请到批准就花去了半年,拍摄又用了整整一年,后期编辑十个月,再加上撰写《西藏一年》一书,前后大概用了三年时间。记者问道:“这么难你为什么还要坚持拍摄?”书云说:“我一直在寻找西藏令人心醉神迷的原因,西藏是我命中注定要去感受和关注的地方。因为,在牛津大学学习时我就已经开始学习藏语,师从已故著名藏学家迈克・阿里斯,在他的影响下,我选择了西藏作为研究方向。牛津大学毕业后,我一次次来到西藏,激动不已,于是萌发了做部片子、写点东西的想法。但是我每次都不能说服自己,因为我觉得自己还没有真正理解,更谈不上准确表达和传递这古老、神秘、独特的文明。”<br />
<br />
&nbsp; &nbsp; 《西藏一年》拍摄的对象是普通的藏民,拍他们一年四季最为真实生动的生活,拍他们外在的、心里的变化,而他们的变化就是西藏的变化。书云说:“我们首先考虑的是寺庙里的僧人。我选择了江孜白居寺管委会副主任次成喇嘛和刚出家的小僧人次平。这一老一少两师徒传佛学佛的过程,生动地展示了藏传佛教的传承。其次是农村。西藏百分之九十的藏族人生活在农村,所以村子里的农民、干部和医生自然成为我们选择的对象。再次是生活在城镇里的居民。片子中的藏族包工头仁青,不仅是藏族经济也是文化的载体,更何况工程、工期、工人、工资等引起的一系列纠葛,会给我们的纪录片增加不少精彩故事。”<br />
<br />
&nbsp; &nbsp; 书云在回答问题前总是浅浅地笑笑。她说:“2005年的西藏之行彻底震撼了我,拉萨的变化可谓天翻地覆。在从拉萨去江孜的长途汽车上,我路过一座座县城,往日简陋的建筑被一座座高楼大厦取代。因为新修了柏油马路,原来从拉萨到江孜需要七八个小时,现在只要四五个小时。以前在西藏旅行时,我习惯于在颠簸的长途车上听人聊天、念经、摇转经筒,现在,车前方安装了闭路电视,左右各一个。左面播放的是印度宝莱坞的肚皮舞,右边播放的是香港武打片。我坐在一角,闭着眼睛,心里问:我是在西藏吗?就是这次西藏之行,让我下定决心拍一部纪录片,用人类学的方法,记录普通藏族人一年四季的生活和他们面临的飞速变化。相信十年之后再审视江孜,就更要回头看我们今天的记录了。”<br />
<br />
&nbsp; &nbsp; 为什么选择江孜来拍《西藏一年》呢?书云说:“因为除了拉萨,江孜是西藏保存最完整的古城,大规模的城市建设和改造热潮并没有冲击到宗山脚下的江孜古城区。没去西藏之前,我有一种错觉,以为青藏高原上多是无边无际的草原,牧民信马由缰,放牧四方,但实际上,百分之八十以上的藏族人生活在农业种植区。江孜城里三条主道的尽头就是耕地,田畴井然,城镇周围分布着许许多多古老的村庄,用格勒博士的话说就是,‘村庄、寺院、城镇三点一线,这就是西藏’。”<br />
