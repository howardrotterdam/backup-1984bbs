首先感谢您的关心，事情经过如下：<br />
钥匙防盗知识：<br />
1、 任何机械的钥匙都会存在一个概率，如汽车的钥匙、房屋的钥匙等，当达到一个数量级别以后，都会出现不同的钥匙可以开不同的车，以及不同房间的门。<br />
备注：大家可以查询国家质量监督管理局相关的文件。<br />
2、 现在一般的汽车都双重防盗，钥匙防盗和电子密码防盗，机械锁（钥匙）是自身的特性，存在一个相似的概率问题。但是电子密码是：现在的汽车钥匙里面带电脑芯片，当钥匙插进汽车点火钥匙以后，钥匙的电脑芯片和汽车电脑就会产生电脑对码，且是滚动对码，当密码信号一致，就可以启动。<br />
整个事情经过：<br />
一、维修历史记录：<br />
粤S-59XX 卡罗拉 李先生 （2010年1月中旬购车） <br />
2010-01-23 拆装/更换后门玻璃 （平安核价237现金出车代交资料）<br />
2010-01-24 拆装/更换前杠、前杠补漆（平安核价300元、现金出车）<br />
2010-02-21 首次免费保养（检查底盘//调远大灯）<br />
粤S-96XXX 威驰 杨先生 （2008年1月中旬购车）<br />
2009年-01-17 第一次免工时费保养（5000KM）<br />
2009年-04-19 第二次免工时费保养（10000KM）<br />
2009年-05-27 拆装/更换前杠、前杠补漆(R)、前叶子板补漆（R）<br />
2009年-09-13 5000KM级别定期保养、更换电动车窗主控开关基板（调紧手刹、机油在后尾箱、使用76人机油免工时）<br />
2009年-12-06 5000KM级别定期保养（自带机油；1、机油不够，差400毫升，请尽快自行添加，切记！2、使用机油卡，免80元工时费）<br />
2010年-02-23 5000KM级别定期保养、拆装/更换前杠、前杠喷漆（单方事故，人保核价1557元（3931#），按人保核价现金出车，代交资料，预约8.5折）<br />
<br />
二、整个事情的过程：<br />
1、2010年1月23日，卡罗拉车主过来更换玻璃，保险SA接待，当时发现卡罗拉和<br />
威驰车的钥匙可以互开，当时前台主管带客户到车间与TL一起进行检查，确认事情属<br />
实，并向客户解释原因，并答应客户马上向丰田进行申请索赔更换全车锁，客户同意，<br />
没有意见。<br />
2、1月23日，TL与FTMS沟通，但负责索赔事项的人员出差，要等他回来才能办理。<br />
3、1月24日，前台主管给客户打电话告知客户说明原因，需要等待时间，FTMS一旦批准我们会及时的通知客户，客户同意。<br />
4、前台主管与客户沟通，并协商好过完春节后回店更换全车锁，客户同意。<br />
5、2月23日，威驰车主做保险，客户询问事情的进展情况，我们告诉客户我们在订货过程。<br />
6、2月25日FTMS正式的索赔申请批准通过。<br />
7、2月27日订货申请。<br />
8、2月28日到货。<br />
9、3月1日前台主管给客户打电话，没有人接电话。<br />
10、3月2日前台主管给客户打电话，客户同意过来更换全车锁。<br />
11、3月2日―3月9日，客户一直没有过来更换全车锁，期间前台主管多次与客户联系，但客户不接电话。<br />
12、3月9日，前台主管给客户联系，客户同意过来更换全车锁。<br />
13、3月10日中午客户给服务经理打电话，要求赔偿或退车，否则将此事情曝光媒体，并且有一个自称是记者的人接了电话，服务经理邀请客户来店友好协商。<br />
14、3月11日下午来店，要求：退车、换车、换锁和5万元补偿，由前台主管和服务经理接待客户，我们不能接受客户的条件，并详细说明保修条款及相关法律规定，客户不同意并说要到电视台曝光。<br />
14、3月12日，公司员工在晚上的南方一台《今日一线》预告得知客户已经曝光此事，并于3月13日晚上播出。<br />
15、3月13日下午，服务经理邀请客户再次来店协商处理，客户下午来店，我们详细说明事情原因及保修的政策，并且告知客户赔偿5万元是不合理的做法，是有敲诈的嫌疑，是要负法律责任的。通过协商客户同意让南方电视台《今日一线》不要播出当晚的节目，当时电视台的答复，已经录制好，无法取消。我们当时也与客户沟通好，帮助客户更换全车锁，并赠送两套全车真皮和两次保养。我们双方签字同意，并当场赠送礼物表示友好，并双方握手言和，同时就谈判过程中出现过激的语言向客户道歉，客户表示理解，并约好第二天上午过来装真皮和换全车锁。<br />
16、3月14日与客户联系，客户不接电话，客户已经在广州进行现场维权，并接受不同媒体的采访，当时服务经理多次打客户的电话，问为什么这样？客户说是受到了我们的威胁。<br />
17、3月14晚上南方一台《今日一线》再次播出对丰田、东部丰田不利的报告，且报道的是单方面的说法。<br />
18、3月15日晚上8点，SA主管邀请客户明天上午9点―10点过来装真皮和换全车锁，客户同意。<br />
19、3月16日截止中午12点，客户暂时没有到店。<br />
备注：以上说法完全属实，有我们订货的记录，有我们打电话给客户的记录，有办公室的监控记录。<br />
