　　他是个天才，首先，他是一个人 <br />
　　 <br />
　　“音乐是什么？音乐是一个大满足。音乐满足了你以后，其他的东西你都不会在乎。”“我不喜欢为了比赛而音乐。音乐是为自己的。真正的享受是在音乐里的，不是比赛，不是赢。”“生和死是什么？”“有的音乐是为了献给神的，但是我是一个人，音乐是给人的。” <br />
　　 <br />
　　说这些话的人，他十七岁。 <br />
　　 <br />
　　“人为什么活着？大多数人都不开心。可是音乐会化解掉所有这些。我想把所有人召集到一起，告诉他们这一点。”“我以后的人生啊，三分之一是音乐，三分之一是生活，三分之一是钱。” <br />
　　 <br />
　　说这些话的时候，他十岁。 <br />
　　 <br />
　　纪录片的时间脉络是他组织了一群小孩参加音乐比赛。纪录片的章节以人物来划分：老师，哥哥，妹妹，父亲，学院。我想之所以用人来划分，也许就是电影一开始，他说的第一句话：“我想做一个人。一个有人性的人。” <br />
　　 <br />
　　他并不可爱。天才就像除草剂，走过的地方，寸草不生。你看到他周围的人为他所遮蔽，所伤。你能看到他同样学习音乐的哥哥，用怎样顺服忍耐的眼神看着他。同样学习音乐的妹妹是如何的沉默，被他偶然夸奖时，露出那样开心的表情。庸人学会了忍耐，但是天才不会。被他指责和批评的同伴，笑着说：“他……他好残忍的！”他最好的朋友说：“为什么我会忍耐他十年？……好问题。是神的安排吧！” <br />
　　 <br />
　　他十岁时，他直接的，真诚的表达不解：“哥哥为什么那么慢？他错那么多！我不能容忍。”他与别人合作，也总是在说：“他们好慢的，等起来好无聊。” <br />
　　 <br />
　　他实在是太自信了，自信到狂妄的地步。他经常弹奏完，自言自语说：“好棒啊，弹得好好！”“我当然不是万人迷，但是我是有魅力的领袖。有魅力的领袖才能改变别人的想法！” <br />
　　 <br />
　　如果说，一个四十岁的人在镜头前说：“钱真的不重要。音乐能满足一切。”我一定在心里说：扯淡呢你吧！但是他十七岁，他说这些话时，无比的笃定，无比的纯粹。那个时候，我不觉得他在扯淡。 <br />
　　 <br />
　　青春也许就是，对未来毫无理由的笃定。以及，真诚的造作，真诚的表演，真诚的夸张。 <br />
　　 <br />
　　没错，他不可爱，但是有些部分，他可敬。比如说，他永远非常真实。问他一个难以回答的问题，他不会闪避，也不会撒谎。他就是戏谑的笑着问：“你真的要知道答案？你真的要我讲出来？”问题关于他父母的离婚。他就用最直接，最简单的话表达自己的看法：“因为我爸爸有外遇。这是我这么多年来，学到最多的一件事。他逃避责任……他没有人性。”镜头里平行剪切的，恰恰是他十岁时，父亲用疼爱到满溢出来的一种手势，不断的不断的，不断的抚摸着他的头。 <br />
　　 <br />
　　比如说，他的特立独行。他永远不为了赢而比赛，只是为了表达音乐而比赛。所以，他选择了超时的曲目，哪怕会被扣分。 <br />
　　 <br />
　　比如说，他拒绝成为职业钢琴家，每个月在国外飞来飞去表演的那种生涯。他在拒绝世俗意义上的成功，拒绝赢。 <br />
　　 <br />
　　比如说，你看他排练，看他表演。他在人群中央转折如意，潇洒自如。那样极端的自信，舒展，陶醉，优雅。那样一种深沉的喜悦，超过了快乐，那样的天然的癫狂。这些不是演的，不是我们常见的钢琴家，动情的一甩头那样的，是完全即兴的。 <br />
　　 <br />
　　看过太多天才的标本，但是这一只是活的。活的，意味着，就像他说的“我是一个人”，他有缺点，甚至有缺陷，有问题，同时，他光彩夺目。活着，意味着，他要经历人世，去体会人间的艰辛和喜悦，无奈和沉重，情欲和禁锢，荒废和压榨。然后，他会明白很多事，会全部带进他的音乐里。 <br />
　　 <br />
　　看纪录片的时候，我常常惊讶于，周围的成年人，有那么高的境界，那么好的耐心去引导他。少年看才情，中年看功力，老年看境界。他周围的成年人，告诉他：“谁说贝多芬一定要快的啊？”“来，说一遍，一个艺术家，除了才华，最重要的是什么？是谦虚。”“表情很重要，表情是你音乐的一部分。”“想象丝绸，湖面，枝条，音乐盒……”他们是人性的。我想，这是他志愿于“做一个人”的种子。 <br />
