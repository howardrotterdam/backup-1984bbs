													<h2>一百个人的十年</h2>
						
						
																											<div id="postmessage_278231" class="t_msgfont">冯骥才以后怎样不清楚，不过这本书个人认为还是不错的。附一篇读后感<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;文革中的作恶者为什么不忏悔？<br />
<br />
　　诸君，你有读到过、听说过在文革中作恶者的忏悔么？既然提出了这个问题，我想你会和我一样陷入困惑，也许还带着哀怒：为什么那些当初在文革中作恶的人、迫害别人的人，没有几个站出来道歉过？这样一场空前绝后的文明劫难，集体忏悔去哪了？<br />
<br />
　　其实这个问题，《一百个人的十年》一书的作者冯骥才先生，在一九九六年的时候已经问过了，然而至今我们――至少我还没有看见过回应。《一百个人的十年》是一本关于普通中国人的文革受难史，属纪实文学。虽然以前我也接触过一些文革史料，但近日读完该书(中国文出版社2008年10月版)，仍然颇受冲击，譬如：一位小学语文教师只因给学生讲了一个“毛主席藏身水沟、摆脱敌人”的故事，竟被揪出来说诬蔑伟大领袖，于是定为“右派”坐牢八年；而这个伟大领袖藏身水沟的故事，其实是这位老师从一本书里读到过、然后讲给学生听而已，但哪一本书他却死活想不起来了；他的文盲妻子为了救夫，便四处找这个故事的出处，只要是张纸，她就去捡来求人读上面有没有那个故事……就这样日复一日，她也就变得疯疯癫癫，最后不幸被火烧死，和她的孩子一起……<br />
<br />
　　无比凄惨――这是《一百个人的十年》中的开篇故事《拾纸救夫》。<br />
<br />
　　最后的结局呢，伟大领袖的那个故事，果然在一本官方的“革命回忆录”里有记载，然而此时这名乡村教师已经坐了八年牢，家破人亡。用故事叙述人的话说，这是一起”千古奇冤”。的确是。<br />
<br />
　　而造成这一奇冤的、以及造成其他许许多多奇冤的，是因为文革，是因为“伟大领袖”，是因为史无前例的“政治崇拜”。<br />
<br />
　　以前读余华的《兄弟》，里面讲到文革中有人拿铁钉砸进自己脑袋自杀的，在《一百个人的十年中》，有一篇《六十三号两个女人》的故事，亦讲到了这个悲惨的自杀方式――“反革命”因不堪忍受污辱折磨，便用这样的方式求得了一死。<br />
<br />
　　《一百个人的十年》是一部普通人的文革受难心灵史，冯骥才在写作这本书的时候，也一定想过希望听到一些忏悔的声音，然而看起来他一样失望。在这本书的前记中，冯骥才写道：我时时想到，那场灾难过后，曾经作恶的人躲到哪里去了？在法西斯祸乱中的不少作恶者，德国人或日本人，事过之后，由于抵抗不住发自心底的内疚去寻短见。难道文革中的作恶者却能活得若无其事，没有复苏的良知折磨他们？<br />
<br />
　　这是一个扔向全民族的大问题，然而党国不回答，也不追究；并且那个最大的作恶者依然被供奉在神坛之上。既然大王还在，小鬼们自然就不怕了，作了恶之后，继续当官当领导的大有人在。这是我们看不到文革有集体忏悔的重要原因。<br />
<br />
　　因为某些个原因，我们对日本人倒是盯得很牢，又是指责人家的历史教科书，又是指责人家的不肯为历史问题道歉――可悲呀，日本好歹还有个东史郎敢公开忏悔，我们呢？在几十年后的今天，中国的战争受害者起码还能向日本政府提起诉讼，要求赔偿，我们呢？我们何时有文革中的受难者发起赔偿诉讼？日本起码向我朝提供了大量援助，我们呢？除了“拨乱反正”恢复个名声，那些受害者还得到些什么？<br />
<br />
　　三年”自然灾害”，再加上十年文革劫难，中国那些年非正常死亡的人数，恐怕和整个二战中死亡的5000多万人不相上下了。若在正常国家，导致发生如此严重灾难的人，恐怕早已被推上了断头台。<br />
<br />
　　中国人真是可悲至极。<br />
<br />
　　如今，当年那些文革中的革命小将，大都也有五六十岁了；而那些文革中的工宣队、军代表们，或许有个七八十岁――他们在哪里？他们忏悔的声音在哪里？他们曾经有过内心的忏悔吗？是不是以为既然罪恶推到了“四人帮”头上，和自己就无关了？仅仅是个忏悔，哪怕只是内心的，可是也没有。<br />
<br />
　　我相信一定还没有什么人发出过那样的声音，一个泱泱大国，竟一个这样的人也没有么？难怪冯骥才说，他虽然倾听过一些良心难安的忏悔内容，但他真正期望的那种不折不扣勇敢的忏悔者，还没有碰到。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;虽然有党国的原因，却也不应该至于如此鸦雀无声。<br />
<br />
　　诸君，也不要统统怪罪到我们的传统文化上去――我们的传统文化中，有礼仪之士，有知耻之士；而经过了文革十年及文革前几年的统治，则彻底“礼乐崩坏”，也难怪在那些腥风血雨中过来的曾经作恶者，还会有多少良知可言――千万别说”在那个时代谁都是受害者”那样的话，这不符合实际。<br />
<br />
　　的确，一个不知道忏悔的民族，是值得怀疑的民族。诸君，不要认为这事和现在没有关系――请想想，汶川大地震发生后，可曾有过一个官员引咎辞职？我是不曾听说过。而你觉得在那样一场巨大的灾害中，难道某些官员不该谴责自己的良心吗？在当今中国，任何一次大的灾害或社会事件发生，我们总是很少看到有敢于出来承担责任、并为责任引咎辞职的官员。所有这些，和文革中的作恶者不道歉、不忏悔，难道不是一脉相承的吗？<br />
<br />
　　冯骥才的《一百个人的十年》开始发表的时候，还是在一九八六年，那时离文革结束仅仅过去十年；在文革发动三十周年那年，他还是没有碰到“不折不扣勇敢的忏悔者”。那么，随着时间的推移，文革中作恶的那一代人都将相继离世，忏悔之门会不会永远关上？<br />
<br />
　　其实文革中那代人许多恰恰还是我们的父母辈，希望上天能给予他们忏悔的勇气。忘了在哪看到的一句话，说“今日中国文革的演员其实都还在，只是缺少文革的导演罢了”。我看这也不是什么危言耸听――对那样深重的文明灾难，竟然连忏悔都没有。对于文革，只有受害者的心灵倾诉却没有作恶者的忏悔，这是严重变态的 “后文革”悲剧。<br />
<br />
