　　　　八九年风波之后，通过前苏联的教训和中国自己的经验，在中国政府、中国知识分子、学生和人民中终于形成了初步的共识，这就是中国不能走前苏联的路，中国要坚持渐进改革的道路。我们在经济上正在推行股份制，开放市场和加入WTO；在政治上正在推动乡村和城市街道的直选，在人民取得经验后，我们将在县一级进行直选。我们正在加强打击腐败的力度，加强法制建设。我们并不认为我们的制度是十全十美的，我们需要不断地改革和改进。我们也并不认为美国的制度是十全十美的，因为经验告诉我们，这个民主的美国总是恃强凌弱。在我们和你们之间有许多灰度，我们需要寻找最佳点来确立我们制度的基点。这就需要我们深入研究和分析各种存在过的制度模型，各种中国制度可能的演进方向和路程，发现人民群众的许多新创造，把所有这些进行对比、排序，以便找到一个最好的目标和路径。这是一个庞大的工程，在我们有了确定的结果之前，我们将坚持现有的制度方式，以保证稳定和持续的改革。 　　　　 <br />
