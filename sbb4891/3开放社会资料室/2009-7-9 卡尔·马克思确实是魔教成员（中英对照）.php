这里又有一个有趣的事实：一名马克思的信徒，Reese 军官，为马克思的死而哀伤，因此去了伦敦拜访他挚爱导师的故居。马克思的家人已经搬走，因此他只能跟屋里的仆人交谈。这仆人所说的关于马克思的话，令他大为惊异：“他是一位对神有着敬畏的人。在他病重时，他惯于在房间里，在一些燃烧的蜡烛前独自祈祷；围绕他头部的伤痕就像带子一样。”这使我们想起了犹太教徒早晨的祈祷仪式，不过，马克思是在基督教会受洗的，他从未声明自己信仰犹太教，而且他后来成了神的敌人。他写了多本反对宗教信仰的书，并把他的所有孩子培养成无神论者。那么，这位未受过教育的仆人理解成祈祷的仪式，到底是什么呢？犹太教徒面对圣物祈祷时，并不需要在面前放置一排蜡烛。那会不会是撒殚教的一种仪式呢？<br />
<br />
Here is one more interesting fact. Captain Reese, a disciple of Marx, grieved by the news of his death, went to London to visit the house where his beloved teacher once lived. The Marx family had already left the home and he was only able to talk to the servant who lived in the same house. He heard the following amazing words about Marx from her: &quot;He was a man with the fear of God. When he was very ill, he used to pray alone in his room before burning candles, wound round his head was something like tape.&quot; This reminds philacteries used by the Jews during their morning prayers. But Marx was baptized in a Christian church. He never confessed Judaism and later became an enemy of God. He had been writing books against religion and had brought up all his children to be atheists. So what was that ceremony which the uneducated servant understood to be a prayer? Praying Jews with phylacteries on their face never place a row of candles before them. Could it have been some sort of a satanic ritual? <br />
<br />
<br />
另一线索在马克思的儿子 Edgar 于1854年3月21日写给马克思的信中。此信开头就是惊人的一句“我亲爱的魔鬼”。一个儿子怎能用如此荒谬的方式称呼自己父亲？不过，撒殚教徒对他们所爱的人都是这样称呼的。难道连他儿子也入教了？<br />
<br />
Another possible hint is in a letter to Marx from his son Edgar, dated March 21, 1854. It begins with these astonishing words, &quot;My Dear Devil.&quot; Where else does a son greet his father in such a ridiculous way? But Satanists write so to the ones they love. Was his son involved too? <br />
<br />
<br />
另一重要事实是，马克思之妻于1844年8月写信给他道：“你最后的牧师信，高级牧师兼灵魂持有者，请将和平与安宁赐予你可怜的羊群。”<br />
<br />
One more significant fact, Marx wife wrote to him in August of 1844: &quot;Your last pastoral letter, Higher Priest and soul Possessor, brought peace and calmness to your poor flock.&quot; <br />
<br />
<br />
在《共产主义宣言》中，马克思清楚地表明他梦想消灭所有宗教。我们可以设想其中也包含了撒殚教的祭仪。马克思之妻称他为“高级牧师”，是哪个教的牧师呢？为何要给这样一名众所周知的无神论者写牧师信？这些信在哪里？马克思生命中的这个时期是尚未被探索的。<br />
<br />
Marx clearly expressed his dream concerning the elimination of all religions in &quot;Communist Manifesto.&quot; We should assume satanic cults were included here too. But his wife addresses him as a Higher Priest. But of what religion? The only faith confessed in Europe where a Higher Priest is present is Satanism. So what kind of pastoral letters could a man write who was known as an atheist? Where are those letters? These are periods of Marx's life that remain unexplored. <br />
<br />
<br />
马克思在绝望中死去，就像所有撒殚教徒一样。他于1883年3月25日写信给恩格斯道：“生命是多么的无目标和空虚，但我多么渴望它啊！”<br />
<br />
Marx died in desperation, as all Satanists die. He wrote to Engels on March 25, 1883, &quot;How purposeless and empty life is, but how desired!&quot; <br />
