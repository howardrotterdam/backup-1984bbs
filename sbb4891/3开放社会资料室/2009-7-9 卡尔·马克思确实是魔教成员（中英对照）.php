													<h2>卡尔・马克思确实是魔教成员（中英对照）</h2>
						
						
																											<div id="postmessage_154432" class="t_msgfont">名词简释：<br />
<br />
Satan -- 撒殚，是犹太基督系宗教里的恶魔之王。<br />
Lucifer -- 路斯弗，堕落天使。<br />
Abyss -- 无底地狱。佛教中亦有阿鼻地狱（Abichi Hell）之说。<br />
<br />
<br />
------------------------<br />
<br />
<br />
以下英文来自：<br />
<a href="http://www.freerepublic.com/focus/f-news/1121228/posts" target="_blank">http://www.freerepublic.com/focus/f-news/1121228/posts</a><br />
<br />
......<br />
<br />
<br />
马克思主义源自一个撒殚教秘密组织 --- 很少马克思主义者知道这一点。<br />
<br />
The origin of Marxism is within a satanic mystery cult - something which very few Marxists are aware. <br />
<br />
......<br />
<br />
<br />
马克思早年是一名基督徒。马克思第一部知名作品叫《基督信徒们依据s翰福音15:1-14而合一：合一的意义、必要性及其影响》。里面有这样的话：“与基督的合一，既在和k紧密而鲜活的友谊之中，又在这样的事实当中：k总在我们眼前和我们心里。同时，我们被k的大爱占据，于是，通过那联合了我们、牺牲了自己的k，我们对兄弟真心相待。”<br />
<br />
In early youth, Marx was a Christian. The first of Marx's known works was entitled, &quot;Unity of believers in Christ according to the Gospel of John 15:1-14: Unity's meaning, unconditional necessity, and influence.&quot; Here we find the following words: &quot;Union with Christ is found in a close and living fellowship with Him and in the fact that we always have Him before our eyes and in our hearts. And at the same time that we are possessed by the greatest love of Him, we direct our hearts to our brothers, with whom He bound us closely, and for whom He sacrificed Himself.&quot; <br />
<br />
<br />
可见马克思知道人们表达兄弟情谊的方式，那就是：通过基督教。<br />
<br />
So Marx was aware of the way in which people may show brotherly love towards each other, that is, through Christianity. <br />
<br />
<br />
马克思继续写道：“因此，与基督的合一，使我们的内在升华，使考验得到慰籍，使我们心灵敞开关爱他人 --- 这不是因为我们骄傲或渴望名声，而是因为基督。”<br />
<br />
He continues: &quot;Therefore, unity with Christ internally exalts, comforts in trials, and makes the heart open to love people, not because of our pride or thirst for fame, but because of Christ.&quot; <br />
<br />
<br />
几乎与此同时，马克思在《一个年轻人在择业前的思考》中写道：<br />
<br />
“宗教授予我们所有人向往的理想。k为全人类牺牲了自己。谁敢否认这一点？若我们选择的职业，能让我们把自己最好的给予人类，我们就不会在其重压下蹒跚行走，因为这是献给万物的牺牲。”<br />
<br />
At the about the same period of time, he writes in his work entitled: &quot;Thoughts of a young man before choosing a profession&quot;: <br />
<br />
&quot;Religion teaches us the Ideal to Whom we all aspire. He has sacrificed Himself for all mankind. Who will dare to deny such assertions? If we have chosen a profession at which we may give our best to mankind, then we won't falter under its burden, because it is a sacrifice for all.&quot; <br />
<br />
<br />
马克思从高校毕业时，他的文凭里注明了他的宗教知识：<br />
<br />
“他的基督教理知识，是明晰、且相当有根基的。而且，他对基督教会的历史非常了解。”<br />
<br />
When he graduated from high school, his diploma contained the following in the category &quot;Religious knowledge&quot;: <br />
<br />
&quot;His knowledge of Christian teachings and principles is clear and properly based. He also knows the history of Christian church to a great extent.&quot; <br />
<br />
<br />
马克思获得文凭不久，一件非常灵异之事发生了。在 Moses Gess 于 1841 年把马克思导向社会主义信念之前，他已成为一名热烈的无神论者。这种性格转变，在其学生时代的后期表现出来。<br />
<br />
Soon after receiving of his diploma, something very mysterious happened. Even before Moses Gess led Marx to socialistic persuasions in 1841, he had become a zealous atheist. This change character could be seen in his later student years. <br />
<br />
<br />
在他的诗词中，马克思写道：“我渴望向上帝复仇。”马克思相信上帝确实存在，虽然上帝从未伤害他，他却要与k争斗。马克思生于一个较富裕的家庭，他在童年时从未挨饿，在学生时代的生活又比他的朋友们好得多。那么，他对神的强烈仇恨从何而来？<br />
<br />
In one of his verses, Marx wrote: &quot;I long to take vengeance on the One Who rules from above.&quot; Marx believed that &quot;the One that rules from above&quot; in fact existed. He contended with Him, although God never harmed him. Marx was from a relatively wealthy family. He didn't starve in his childhood and in his student years he lived much better than his friends. So what caused his fierce hatred towards God? <br />
<br />
<br />
我们尚未知晓其个人动机。或者，在这挑衅的声言中，马克思只是“另一位”的喉舌？<br />
<br />
Personal motivations are not available to us. Maybe Marx was only somebody's else speaker in this defiant assertion? <br />
<br />
<br />
在此时期，马克思的一首诗《堕入绝望的魔咒》中写道：<br />
<br />
“我将在上苍设立我的王座，寒冷与恐惧是其顶点，迷信的战栗是其基座，而其主人，就是那最黑暗的极度痛苦。<br />
<br />
以健康观点看待世界的人，将会转变，变得惨白和死寂。他被盲目和寒冷的死亡所占据，将给他的快乐准备坟墓。”<br />
<br />
During this period, the following lines are taken from him from the poem entiled: &quot;Conjuration of falling into despair.&quot; <br />
<br />
I'll set up my throne above, Cold and terrible will be the peak of it. Superstitious trembling is at its base, Master - most black agony. <br />
<br />
The one who will look with healthy looks, Will turn away, turn pale and deadly mute. Possessed by blind and cold deathness, will prepare a tomb with his happiness. <br />
<br />
<br />
坐于王座上的“那一位”，将散布极端的痛苦与恐惧 --- 这个自白和“我将在上苍设立我的王座”一句，使我们想起了路斯弗的骄傲之言：“我要升到天上，在神的众星之上，我将设立我的王座。”（圣经・旧约・以赛亚书 14:13）<br />
<br />
The words &quot;I'll set up my throne&quot; and his confession that agony and fear will go forth from the one who is sitting upon the throne, remind us Lucifer's proud boast: &quot;I will ascend to heaven, higher than God's stars I will set up my throne&quot; (Isiah 14:13). <br />
<br />
<br />
为何马克思需要这样一个王座？马克思在其学生时代写的一个声名狼藉的剧本中，有着答案。这个剧本叫《Oulanem》，里面提及撒殚教的“黑色聚会” --- 这种祭典仪式由一名祭师带领，于午夜时分焚烧一部《圣经》；聚会者发誓要犯（罗马天主教教义中的）全部七宗致命大罪，且永远不做好事；然后，他们进行纵欲狂欢。<br />
<br />
Why did Marx need such a throne? The answer for this question is in an infamous drama written by Marx in his student years. The drama is called &quot;Oulanem.&quot; There is a mention of a satanic &quot;Black mass,&quot; a ritual conducted by a priest at midnight in which a Bible is burned. All present promise to commit all the seven deadly sins mentioned in the Roman Catholic catechism and to never perform any good works. An orgy follows after that. <br />
<br />
<br />
撒殚崇拜由来已久。在《圣经・旧约・申命记》中我们读到，那些人“向魔鬼献祭”，然后，以色列之王 Jeroboam 设立了高位、牛羊的祭师。<br />
<br />
Worship of Satan is very old. In Deuteronomy we read that the Jews &quot;made sacrifices to demons&quot; (32:17). Later, the king of Israel, Jeroboam, set up priests of the high places and of the goats and bulls that he made&quot;(2 Kings 12:25-33). <br />
<br />
<br />
为了理解《Oulanem》，我们可以读一下马克思在《Nidler》一诗中的奇异自白：<br />
<br />
“地狱之气升起并充满我的头脑，这将使我发疯，并使我的心发生戏剧性变化。看见这把剑了吗？黑暗之王把它卖给了我。”<br />
<br />
The &quot;Oulanem&quot; can be understood if we read with Marx's bizarre confession made in the verse &quot;Nidler&quot;: <br />
<br />
Hellish evaporations rise and fill my brains, Until I will go mad and my heart will not change dramatically. See this sword? The King of darkness sold it to me. <br />
<br />
<br />
这些字句有特殊意义：在撒殚教的晋阶祭仪中，一柄施了巫术、能确保某种成功的剑，会被卖给晋阶者。而晋阶者付出的代价，就是用他血管里的血在恶魔契约上签字，于是，在他死后，他的灵魂将属于撒殚。<br />
<br />
These lines have special meaning when we take into account that during the rituals of higher dedication into a satanic cult, a bewitched sword that guarantees a success is sold to the candidate. He pays for it by signing with his blood taken from his vein the contract which makes his soul belong to Satan after death. <br />
<br />
<br />
