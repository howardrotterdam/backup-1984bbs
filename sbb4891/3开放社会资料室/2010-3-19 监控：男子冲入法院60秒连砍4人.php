坐在大厅内的保安刘育红像往常一样，站起来问：你好，请问你找谁？何胜凯没有回答，而是突然朝他腹部连刺两刀。大厅里当时还有办事的群众，见状吓得赶紧跑出来，刘育红事后回忆说，“他只杀穿制服的，以为我们保安是法院的人。”刘育红其实属于保安公司管理，才来法院上班4个月，只有27岁。他遭遇袭击时，同值班保安璩先武刚上楼去买饭票。刘育红奋力反抗，左手抵挡，右手试图夺走何胜凯的刀，于是左臂又中两刀。这时，正在值班室的法警支队队长钟世鑫从监控屏幕中发现了这一幕，他所在的值班室在大厅的一角，一面墙壁是块大玻璃，透过玻璃可以看到全院的所有监控录像。钟世鑫军人出身，身体敦实，个头、体形都比何胜凯高大。据法院内观看了内部监控录像的人讲述，钟世鑫一把抱住何胜凯，何胜凯的刀掉在地上，这时刘育红身中4刀仍试图捡起刀刺向何胜凯，可因失血过多而倒地。第一把刀掉地后，何胜凯迅速从“屁股上”拔出了另一把刀，刺向钟世鑫。钟世鑫和何胜凯搏斗的录像警方没有公布，“太残忍了。”钟世鑫今年47岁，长得一副娃娃脸，工作勤勤恳恳，事发的前一天晚上他到8点多才下班。在他的遗体告别仪式上，市中院院长赵传灵评价他“和气、热情、厚道”。吴明华等两位老上访户也给他送了花圈，“他从来不吼我们，夏天外面太阳大，我们在外面站着，他叫我们进去坐，冬天外面冷，他让我们要注意感冒。”何胜凯拔出第二把刀的一幕同样也被正准备从一号审判庭出来的王义碧看到，一号审判庭的大门正对着案发现场，她尖叫了一声“杀人了”，便吓倒在地，畏缩在一号审判庭最后一排座位边的地上，双手抵挡何胜凯的袭击，她左手中了两刀，右手中了3刀。钟世鑫倒地后，何胜凯往值班室走去，这条通道只通向法院老楼，楼下是接访室。过去，接访室在一楼门外，不需上台阶，前不久，全国农村精神文明建设工作经验交流会在遵义召开，遵义会议旧址附近的楼房及地面装饰一番，而法院的接访室也关闭了外面的大门，改为地下室。这时，买完饭票的璩先武正好也走到值班室，何胜凯猛地刺向他，璩先武双手一推，刺向他的刀没有伤及内脏。刺完这一刀后，何胜凯拔腿就跑，逃出法院，三步并两步飞奔下台阶，像皮球一样撞在停放在台阶下的一辆汽车上，一个正在打电话的人的手机也被撞飞。爬起来后，他跑过马路，又摔了一跤，马上爬起来，继续跑。在法院斜对面的巷子口，他顿了一下，迅速拐进去。约一分钟后，法院内跑出一个人，追出来，可何已不见踪影。从进入监控范围到跌跌撞撞逃离现场，何胜凯只用了60秒，时间是10月14日下午4点23分40秒至24分40秒。<br />
<br />
3天后，当地警方公布了从监控录像中截取的图像，并悬赏5至10万元征集线索。10月18日中午，警方将何胜凯从家中带走，10月19日上午，何胜凯被押送到遵义市中院指认现场，前后也不到60秒，何被4名警察抬手抬腿搬离现场，门外上千群众拥簇围观。<br />
<br />
心有天下的人<br />
<br />
2008年3月25日，何胜凯服刑两年零六个月后释放回家。在离释放不到两个月时，他写了一篇《放开心胸，迎新生》的文字，文章中自问，“何以未成为国家栋梁，却来到这里学习反省，成为了人民的罪人呢？”“国家对我们的教育一点点效果都没有吗？”他的回答是否定的，“不，我们也曾‘我本善良’，人之初，性本善。”他为之自豪的共和国始终是他思考问题的主线，他希望“民族有望强于林，国家有望招环球”。在另一篇文字中，他称自己是一个“心有天下的人”，“生不为惜，只可未为国之捐躯，荡平污垢。死未所憾，英雄虽死贼仍狂。憾。一腔热血未君识。憾！”释放前的那封信放在一个写给《贵州省监狱工作报》的黄皮纸信封中，但没有提到自己的“冤屈”。折叠在一起的一封写给遵义市中级人民法院的信却详达自己在派出所和法院的不公平遭遇。“威严的人民法院竟然给我鸳鸯判决书，给我进行了篡改。”这是他写的信中对自己“冤屈”的简略归纳。本刊记者向有关部门了解，一名官员解释说，“判决书当场签字才生效，以第一次当庭签字为依据，如果签字后又反悔那肯定不行。”他的二姐何胜先曾去监狱看他，何胜凯让她请律师申诉，“他心里不服气”。他的姐姐说，弟弟在监狱里不断地申诉，却被关禁闭。出狱后，他继续向遵义市各级法院、人大，省政法委等部门申诉，可都石沉大海。他的申诉信中满怀对国家和民族前途的忧虑。“法官是社会正义和良知的最后一条底线，竟然如此腐烂、卑劣，不铲除大乱之期不远。”今年国庆的头两天，他在家看了两天的阅兵式。他在信笺上写下，“六十年之大治，国民之深望。”从9月份至10月份，他反复在修改一封信，“一为天下苍生，二为共和国永存”。这封信他要寄给国家主席。他觉得这个国家“树大难免有枯树枝，要进行打扫卫生的清洗工作”。信件的结尾一句是“不旗帜鲜明地反腐……国家和民族将受到不测的命运。”他的房间里有他近期读的书，包括拿破仑、希特勒、邓小平的传记，还有一些诸如《神到底存不存在》等人生宗教哲理的书籍，他还摘抄王阳明的诗句。他很少看电视剧，却经常听歌，最喜欢听的是美国萨克斯演奏家凯丽金的曲子。<br />
<br />
他崇拜伟大人物，崇尚英雄气概。在为国家民族命运忧虑的同时，觉得自己所能发挥的作用是：“为天下苍生请难，引刀为一快，不负少年头。”<br />
<br />
精神错乱<br />
<br />
何胜凯把自己的人生归纳为“生于贫贱，长于磨难”。还只有八九岁的时候，他的父母离婚，他和二姐跟父亲生活，大姐和三姐跟母亲生活。可他的父亲生性嗜酒，在他二姐的记忆中，父亲“从小就整天喝酒，不管我们”。七八岁的时候，何胜凯就要自己弄饭，小学的学费也是姐弟俩放暑假捡破烂挣来，后来二姐先退学，挣钱支持何胜凯读完了初中。嗜酒的父亲2000年死于伤寒，他和二姐又回来和母亲一起生活。这时，地处遵义市郊的坪丰村的经济逐渐发展起来，土地越来越值钱。后来，他母亲盖起了楼房，靠租房度日。而村内的土地纠纷日益增多。在母亲的帮助下，2003年左右，何胜凯有了一段失败的婚姻，仅仅维持了20余天。他的新婚妻子回门后，一个女人找到他家来闹，说何胜凯的新婚妻子一直在和她丈夫鬼混。何胜凯和他的家人都受不了如此的“刺激”，“东拉西扯就离了婚”。此后的挫折则让他的人生发生了根本性改变。2004年，他们家和邻居唐坤地家发生纠纷，按何胜凯家人的说法，邻居占了他们家30平米左右的地方建房子，起初答应给钱补偿，可后来却不给。双方发生纠纷，有一次，何胜凯和对方打了起来，何胜凯拿了刀子将对方刺伤了，对方住院六七天才出院。“当时，弟弟跑了，他被吓得要死，我们劝他回来，说事情总要解决。”何胜凯的大姐说。对此事的叙述出现在何胜凯给市中级人民法院的申诉书中，他认为当时是自卫，“他骂我老子还怕你这龟儿子，让我饱尝老拳，我奋起抵抗自卫。”他还控诉长征派出所一警员对他刑讯逼供，“将我吊扣4个多小时……他写材料，提着我的手乱盖。”本刊记者向有关部门求证，对方称已听说这一情况，目前还在核查中。何胜凯的姐姐说，弟弟咨询过律师，他那种情况一般是判6个月，他觉得是派出所做了“黑材料”才判那么重。更增添他的不公平感的是，对方是生产小组的会计，他认为这一身份才导致派出所不公平办案。“资源的不对称、地位的不平等，我知道，我所能动用资源太悬殊，正义不仅是迟到，而是永远的缺席。出狱一年多后，家人让他开了个文具店，他也办了驾照，重新开始新的生活。可一次挫折彻底改变了他，好心的邻居给他介绍女朋友，姑娘长得也算不错，可他却把人家赶走了。此后，他“就像撞了鬼一样”――他不吃家里和餐馆里的任何东西，饮水机的水也不喝，宁愿放自来水管里的水喝。他经常对家人说，“四面八方都有陷阱，有人要下毒陷害我。”他的房间里至记者到访时留下了半箱方便面，这是他两个月来的主食。</div>

							
							
							
							
