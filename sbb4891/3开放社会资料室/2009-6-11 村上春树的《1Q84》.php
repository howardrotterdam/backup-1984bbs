													<h2>村上春树《1Q84》来了</h2>
						
						
																											<div id="postmessage_336503" class="t_msgfont">11月13日，村上春树新作《1Q84》繁体版正式在台、港、澳三地上市。继上一部长篇小说《海边的卡夫卡》之后，时隔七年村上再次推出新作，究竟它与名著《1984》有何联系？村上是否在其花甲之年，终于写出具备竞争诺贝尔文学奖实力的作品？南都周刊特约记者・ 田志凌<br />
 <br />
　　今年5月《1Q84》在日本发行后一度引发抢购热潮，曾创下两个月内两册书狂销两百万册的纪录。韩国出版社也以高达15亿韩元（约840万元人民币）的天价争得版权。<br />
 <br />
　　令国内村上春树的粉丝们失望的是，简体中文版版权至今杳无音讯。之前最被看好的新经典文化公司，据内部人士告诉记者，目前简体版权仍然悬而未决，他们仍在等待日本方面的答复。如果算上翻译和出版时间，则内地读者要看到简体中文版《1Q84》还遥遥无期。<br />
 <br />
　　繁体中文版此时在港澳台上市，无疑备受关注。究竟《1Q84》讲了什么样的故事？它为何引发如此大的关注？村上春树是不是再次突破了自己？本刊日前专访了繁体版村上春树的译者赖明珠。<br />
 <br />
　　赖明珠是引介村上春树进入台湾出版界的早期推手，从上世纪80年代开始翻译村上春树的小说，已经翻译了16部，目前总销量超过100万册。<br />
 <br />
　　《1Q84》是村上春树“达到成熟阶段的集大成作品”<br />
 <br />
　　《1Q84》以20世纪60年代末70年代日本赤军连学生运动，延伸到80年代奥姆真理教等社会事件为故事的经线，男女主角的恋爱故事为纬线，编织出丰富多彩的村上式小说世界。<br />
 <br />
　　时报出版社此次推出《1Q84》上下两册繁体版首印20万套。据时报出版社介绍，自9月底小说开始预购至今，上下两册预购量共2.2万套，已经超过了日本。首印20万套中平装本17万套，精装本3万套，也创下台湾小说出版首印纪录。<br />
 <br />
　　南都周刊：在您看来，《1Q84》是一部什么样的作品？<br />
 <br />
　　赖明珠：它是村上写作生涯30年后，达到成熟阶段的集大成作品，也是他所有长篇小说中最长的一部。人物角色更多，涵盖年龄层由出生到死亡，写作手法包含写实和超现实，结合推理小说、历史小说、爱情小说的特色，是一部可以从多方面解读和欣赏的综合小说。<br />
 <br />
　　南都周刊：《1Q84》这个书名透露了什么样的讯息？<br />
 <br />
　　赖明珠：村上春树的作品看似简单，其实非常耐人寻味。光以书名为例，就大有学问。就像从一粒沙可以看一个世界一样。<br />
 <br />
　　《1Q84》由阿拉伯数字和英文字母组成，书名也点出村上文学的特色之一:异质性的组合。有人说他“和魂洋装”，相当贴切。<br />
 <br />
　　《1Q84》是从《1984》来的，《1984》是乔治・奥韦尔写于“1948”年的预言小说，因此书名就把“48” 倒过来成为“84”。出版于1949年，正是村上诞生的那一年。《1984》出版至今正好60年。和村上春树同龄，村上今年出《1Q84》这本小说，可以说和《1984》一起庆生。特别向奥韦尔致敬。<br />
 <br />
　　呼应《1984》书中的Big Brother的极权专制，篡改历史的情节，和半世纪以来世界各地至今仍战争不断的愚昧，和盲目崇拜宗教的行为。村上以《1Q84》的“Little People”对应，希望唤起读者自觉和警惕。希望像奥韦尔书中的极权专制和像奥姆真理教般的问题不要重演。他选择《1984》这一年当书名，但把9改成Q。书中提到Q是英文Question mark的Q。1Q84是从1984分出来的另一个新世界。他用一些象征性的描写，刻意强调Q字，让读者感觉不解、怀疑，是希望读者凡事要自己多思考。<br />
 <br />
　　“村上春树的生命已经到达‘天凉好个秋’的圆熟阶段”。<br />
 <br />
　　小说的故事以双线进行，奇数章写女主角青豆，偶数章写男主角天吾。青豆和天吾是小学三四年级的同班同学，十岁时互生好感。然而二人个性孤独没说过几句话。随后青豆转学，两人失去联络，二十年后才又重逢。在分开的二十年里，男女主角各自经历一番波折的爱情，三十岁的现在彼此竟然都没有忘记十岁时的对方。这令人想起村上早期的作品《遇见100%的女孩》。<br />
 <br />
　　长大后天吾认识另外两位女性，一位是别人的妻子比他大十岁，一位是和他合写小说《空气蛹》的十七岁美少女，都是掌握主导权的女性。天吾反而经常是被动的。《1Q84》在各方面，都和既有的“男尊女卑”、“男强女弱”的价值观逆转。<br />
 <br />
　　书中有纯爱，有大胆奔放的性爱，有不伦、背叛、校园虐待、家庭暴力、同性恋、离家出走、失踪等各种离奇情节。<br />
 <br />
　　南都周刊：《1Q84》和村上春树以前的作品有什么大的不同？<br />
 <br />
　　赖明珠：去年11月我访问村上春树时，问他觉得自己三十年来有什么改变？他说以前他写得辛苦，想写的东西只能表达一部分，现在可以随心所欲地写了，因此也越写越长。<br />
 <br />
　　在《1Q84》中天吾说：<br />
 <br />
　　“写小说时，我用语言把我周围的风景，转换成对我比较自然的样子。也就是重新改造。这样做，以确认我这个人确实是存在这个世界的。如果那作品唤起许多人的同感和共鸣的话，那就成为拥有客观价值的文学作品。”<br />
 <br />
　　基本精神没有变，只是现在他对事情看得更深、更透，也写得更具体了。此外，以前村上春树的作品经常以第一人称写，本书以第三人称写，更添客观视野。<br />
 <br />
　　南都周刊：有评论认为，这部小说蕴涵着村上的“多重世界观”，您认为是否存在“多重”？<br />
 <br />
　　赖明珠：确实，就像“俄罗斯套娃”那样，大套中，中套小。他喜欢故事中含有故事，并随时加入小枝节，小比喻。《1Q84》小说中含有小说《空气蛹》，主角天吾改写《空气蛹》准备应征文学奖。《空气蛹》中又含有各种小角色。形成耐人寻味的多重境界。<br />
 <br />
　　《1Q84》中，也提到英国狄更斯的《孤雏泪》、俄国契诃夫的《萨哈林岛》(库页岛)、日本古典文学《平家物语》。意象丰富，趣味横生。<br />
 <br />
　　不只是文学，在音乐方面，从第一章就写到青豆在车上听到杨纳杰克的《小交响曲》勾起童年的回忆，顺便提到作曲家的创作故事。中段提到《马太受难曲》、Sonny &amp; Cher男女二重唱等。村上总会告诉我们如何欣赏一些美妙的音乐。甚至和400年前的人一起欣赏。<br />
 <br />
　　他的小说总能以意识流的穿插，以爵士音乐的自由气氛，借着故事和对白，带给年轻人许多文化、历史、自然、艺术各方面的知识。<br />
 <br />
　　南都周刊：据说新书相较于村上以往作品更多地关注现实世界和社会问题，较少关注个体，因此书中也少了很多物质符号，风格更为硬朗，您怎样认为？<br />
 <br />
　　赖明珠：这几年来他透过写作，可能自己也获得了疗愈作用。作品不再像年轻时那么多愁善感了。也可以说他的生命已经到达“天凉好个秋”的圆熟阶段，对社会对读者他有更多的关注。不过小说还是从男女主角，天吾和青豆的眼光和感受来描写的。正好这两个人对物质欲望都低，喜欢简朴生活，因此在他其他作品中常出现的外来语物质符号大量减少了。<br />
 <br />
　　“他最了解什么最吸引观众”<br />
 <br />
　　村上春树的文字魅力是其作品吸引如此多读者的重要原因之一。赖明珠指出，村上春树是学戏剧的，最了解什么最吸引观众。他的小说像电影一样充满视觉上的美感，听觉上的节奏感和故事高潮迭起的情节。在《1Q84》中，这种文字的“深入浅出”得到了充分体现。<br />
 <br />
　　赖明珠的中文译本常常被拿来与林少华、施小炜作比较。林译的华丽婉致被看过日文原著者指责为“添油加醋”，赖译的直白活泼又被部分林氏村上迷评价为“难以卒读”，施译则被一些读者认为太拘于原著而束手束脚。同时每个译者也都有他的支持者。<br />
 <br />
　　比如在《遇见百分百女孩》中有一句话，林少华颇为斟酌地译作：“或许你也有你的理想女孩。例如喜欢足颈细弱的女孩，毕竟眼睛大的女孩，十指绝对好看的女孩，或不明所以地迷上慢慢花时间进食的女孩。”<br />
 <br />
　　赖明珠则译为：“或许你有你喜欢的女孩类型，例如你说小腿纤细的女孩子好，也许非要手指漂亮的女孩才行，或者不知道为什么，老是被吃东西慢吞吞的女孩子所吸引。”后者干脆浅白的风格，翻译专家余中先认为是最接近原著的。<br />
 <br />
　　南都周刊：译完《1Q84》之后，您喜欢它吗？在您心目中，这本小说最精彩的地方在哪里？<br />
 <br />
　　赖明珠：或许因为村上春树是学戏剧的吧，我觉得他对人物的描写特别生动。<br />
 <br />
　　例如：编辑小松<br />
 <br />
　　“小松这个人有某种深不可测的地方??他是依自己的理论思考事情，下判断的类型。他不会做不必要的炫耀，但他读大量的书，对分歧的各方面都拥有绵密的知识。他还能凭直觉看穿别人，拥有挑出好作品的慧眼。其中虽然含有相当程度的偏见，不过对他来说，偏见也是真实的重要要素之一。”<br />
 <br />
　　对人物造型的想象，也是阅读村上春树作品的一大乐趣。<br />
 <br />
　　南都周刊：总结来说，村上春树作品的文字魅力在哪里？<br />
 <br />
　　赖明珠：村上的用字基本上浅显，但有时也晦涩、暧昧、深沉。时深时浅，时快时慢，形成活泼生动的节奏感。<br />
 <br />
　　内容除了大部分容易懂之外，一定还会留下小部分暗影，像看不透的谜，陷入他的文字迷宫，幻想情境中，一时出不来。<br />
 <br />
　　村上春树的博学多闻，可以说是成长背景所培养出来的。<br />
 <br />
　　父母亲是国文老师，小学时就要他熟背《枕草子》、《徒然草》、《平家物语》等日本古典名著，餐桌上的话题是《万叶集》。奠定了他深厚的日文基础。<br />
 <br />
　　中学时代父母为他订购《世界文学全集》、《世界史》。他可以到家里附近的书店签账买书。读神户高中时代，开始到旧书店买船员留下的英语小说，听西洋歌曲，看世界电影。吸收大量的西方文化艺术。并开始尝试把英语翻译成日语。<br />
 <br />
　　大学读早稻田戏剧系，又在戏剧博物馆大量熟读剧本。还没毕业就开起咖啡厅，每天听客人谈人生百态。<br />
 <br />
　　这样的成长背景和生活方式，培养出他蕴藏丰富的文学矿脉。<br />
 <br />
　　由于他是学戏剧的，最了解什么最吸引观众。他的小说像电影一样充满视觉上的美感，听觉上的节奏感，和故事高潮迭起的情节。他把自己的感觉透过洗练的文笔，像对朋友述说般，真诚地直接传达到读者的内心。<br />
 <br />
　　例如有一段，这样描写月亮：<br />
 <br />
　　“都市世俗的灯光，像平常那样消去星星的踪影。天空虽然晴朗，却只能看见几颗特别亮的星星，淡淡地分散在一些地方而已。虽然如此只有月亮还是可以清晰地看见。月亮对照明、噪音、污染的空气都没有一句怨言，依然规矩地浮在那里。只要睁大眼睛，也可以辨认出那巨大的陨石坑和山谷所形成的奇妙影子。在无心地眺望着月亮的光辉之间，天吾心中从古代所承继下来的记忆般的东西被唤起了。从人类得到火和道具和语言之前开始，月亮已经不变地站在人类这边??”<br />
 <br />
　　在写作之初，他就体认到“想写不一样的东西，必须用不一定的语言。”因此他非常重视文体，叙事方式自成特色。<br />
 <br />
　　文浅意深，是翻译村上最大的困难<br />
 <br />
　　赖明珠曾经去拜访过村上春树两次。实际见面的感觉是“人如其文”，村上与他的文章和书中主角“我”给人的印象很接近――完全不摆架子，从容自在，态度自然，说话幽默，相当平易近人。不过也看得出是个很有个性和原则的人。当然，赖明珠认为，要了解一个作家，读他的书才是最好的方法。书中所写的事情，绝对比短时间见面所谈的更深入。<br />
 <br />
　　南都周刊：您觉得村上的作品容易翻译吗？在翻译过程中，您最大的快乐和困难分别是什么？<br />
 <br />
　　赖明珠：我开始翻译他的作品时，他还没有现在这么大的名气，因此是以比较轻松的心态开始的。起初觉得他用字简洁，应该不难翻译。他早期的作品句子确实比较简短。基于想把好东西跟朋友分享的出发点，觉得自己只是在做一件自己喜欢做的事情。他的作品渐渐受到大家的欢迎，感觉很欣慰。<br />
 <br />
　　但后来他的小说有越来越长和越来越深的转变。幸亏基本上他的文法结构清晰、思考逻辑合理，就算有些句子长一点，只要仔细阅读应该不难理解也不难翻译。比较困难的地方是，他喜欢用简单的文字表达难以用文字表达的感觉，这在翻译的时候，必须很用心去揣摩，以免误解或遗漏了某些细腻和微妙的地方。<br />
 <br />
　　最大的快乐是把他的特殊文体转换成中文还能保持那特殊文体的感觉。<br />
 <br />
　　最困难的是他的外来语范围很广，从音乐曲名、电影片名、人名到商品品牌、烟酒食物名称，有时查很多数据都查不到。现在有网络方便多了。<br />
 <br />
　　但最担心的还是误译或漏译。如果校对时疏忽，印出来才发现，只能在增刷时修正。《挪威的森林》和《听风的歌》改版时，也趁机删掉许多赘字。幸亏村上春树自己也翻译美国小说，在《翻译夜话》书中提到他翻译时也犯过错。只是随着他的名气越来越大，身为译者责任感也加重了。<br />
 <br />
　　南都周刊：以村上作品而言，台湾读者和大陆读者在中文阅读习惯上有何不同？这会影响您的翻译吗？<br />
 <br />
　　赖明珠：最大的不同是台湾用繁体字，大陆用简体字。不知道是不是受字体影响。台湾读者感觉大陆翻译比较简化。大陆读者可能也会觉得台湾版本不够精简。<br />
 <br />
　　我发现大陆明星和选手名字不少是两个字的。下一代会不会越来越多人取两个字的名字。例如大陆的王菲、巩俐、周迅、汤唯、姚明。台湾的邓丽君、周杰伦、张惠妹、王健明。香港的周润发、梁朝伟。虽然只是一字之差，却相差了三分之一，令人忧心。不过当然也有例外，像章子怡、舒淇、成龙。<br />
 <br />
　　字体的繁简，是否也会改变生活习惯和个性，连带波及翻译？大陆文革时，台湾正在推行文化复兴运动，也极力维护繁体字。有时我感觉有些简体字太简化，有些繁体字笔画太多，反而日本汉字比较适中。<br />
 <br />
　　我在翻译时比较在意忠于原著。虽然获得多数读者支持，但也有人反映不像中文，或太 嗦。不无道理，我也常提醒自己，要精简一些。<br />
 <br />
　　上次在香港和东京和林少华先生见了两次面。因为同是村上译者，一见如故。我也拜读过他的译本，对四字成语之多印象深刻。谈话间，我发现他习惯出口成章，因为他喜欢唐诗，所以用那样的语言翻译感觉最自然。我说也许“文革”之后，大家渴望恢复旧文化，他的译本正好有疗伤作用，因此特别受喜欢。他想了一下，点头说，也许有道理。（本文中涉及文学名词均引自台湾译法）<br />
 <br />
　　超越纯文学，走向“新现实”<br />
　　文・ 文琳<br />
<br />
 <br />
　　村上春树曾在《读卖新闻》上自白，“我想要写的是立体地描绘出这个时代的整体世相的综合小说，超越纯文学，在时代的空气中描绘出人的生命存在感。”“我会变老，读者也在成长，而通常作家年纪越大就能越好地描写他的时代。”<br />
<br />
 <br />
　　这部被认为是向奥威尔致敬的作品，一开始就有怀疑是否同样是部“批判之作”的评论出来，但作家的职责却大概不在“批判”，而在于“传达”。《1Q84》传达的是蕴藏在复数可能性当中的“真相”，用虚拟来表现真实，以“假”来说“真”，这样的写法本身就和村上的文学态度转变联系起来。<br />
<br />
 <br />
　　而他也明确说出自己开始思考小说中的现实性这个问题是在9・11之后。这样一个故事构思出来并写了七年，时间上也确实与9・11事件吻合。《1Q84》中教主深田保以及青豆的“神圣”使命的情节也容易让人找到奥姆真理教事件的印记，对60年代末学生运动的回忆更暗示出村上内心深处的政治记忆。这些记忆共同传达出什么信息，对村上来说，这些事件乃是昭告“现代”这个意象的破灭，像月球表面丑陋的阴影，村上要做的只是让更多的人从“现代”的“现实”中清醒过来。<br />
<br />
　　90年代之后，美苏体制不再，中国改革开放，“global化”全球一体，这样一些教科书中的表述到底具有什么意义，村上的理解是，这同时代表的也是一种否定性思维的消失。面对资本化的现代体系，在马克思主义之外，现在有没有可以替代的否定性思维，这个问题并不被重视。<br />
<br />
 <br />
　　因此，思想上的转变直接影响到村上的文学理解。村上春树尽管已经写了三十年，但他在日本文学中的地位，其实还是颇为尴尬，读者的捧场并没有为他赢来文学界内的一致认可，作品也一直在纯文学和大众文学两个标签之间难以归档。现在村上自己站出来表态，提出要超越纯文学并要开始写作“新现实小说”，新现实小说这个概念本身就是对传统的纯文学和大众文学概念的否定。<br />
<br />
 <br />
　　日本文学史上的纯文学概念产生于1893年，由作家北村透谷提出来，并被定义为“非为学问而将重点放置于美的形成的文学”。在1960年代和1990年代，日本文坛也先后掀起过两次关于纯文学的论争，其中一个论点是，纯文学可能吗。纯文学在最初是相对于直接与经济社会印刷出版业挂钩的大众文学的一个概念，强调对人的纯粹的精神生存状况的表述。而文学界开始对纯文学之可能性的反问，实际上是要引出另一个答案：在现代语境下的纯文学是不可能的。在人的精神生存遭受经济和政治社会全面威胁的背景下，纯文学必然地要被超越而最终面向“现实”，而这个“现实”并不是一般人所了解或者所能看到的现实，作家之所要传达的正是在此，这也是村上所说的“新现实小说”。<br />
