													<h2>村上春树新书疑似山寨版1984</h2>
						
						
																											<div id="postmessage_122157" class="t_msgfont">路透东京6月10日电(记者Yoko Kubota)---日本知名作家村上春树凭借《海边的卡夫卡》等复杂的超现实主义作品受到全世界读者的欢迎。当下，沉寂了数年的村上春树终于推出了他五年来的第一部作品《1Q84》，并在日本掀起了抢购狂潮。<br />
<br />
<br />
无论是通勤者还是博客博主，似乎每个人都在翻阅这本上下两卷，共1,055页的硬皮版着作。《1Q84》是2004年村上春树出版《天黑以後》之後的第一部小说。<br />
<br />
<br />
《1Q84》讲述了发生在日本东京1Q84年的事情。书名借鉴了乔治&#12539;奥威尔(George Orwell)的小说《1984》，因为在日语里数字9的发音和英文中的字母Q接近。<br />
<br />
<br />
《1Q84》在两周前与读者见面，随後即是报纸、电视和网络对这本书铺天盖地的评论。预计到月底前，此书将有超过100万本上架，出版社表示对于文学作品来说，这个印数已经是相当高了。<br />
<br />
<br />
“虽然另外有些书卖得更多，但是文学作品能够卖出5万本，我们就叫畅销书了。卖出10万本，就是巨大成功，”出版方新潮社的发言人Fumiaki Mori说，“发行仅仅10天就能达到这样的数字，速度是非常惊人的。”<br />
<br />
<br />
此书的主人公为一男一女，主题包括崇拜、虐待、失落、性、爱和谋杀。<br />
<br />
<br />
《1Q84》在很多家书店内已经售罄，其热销还带动了相关音乐和奥威尔《1984》的销售。<br />
<br />
<br />
出版社的Mori说：“我们已经很长时间没见过一本小说对社会产生如此大影响，或一本小说的出版成为如此瞩目的社会事件了。”<br />
<br />
<br />
Mori称，有关村上春树有望获得诺贝尔文学奖的媒体报导可能也有助于这本新书的销售。（完）<br />
<br />
