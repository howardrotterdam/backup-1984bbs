													<h2>村上春树新作《1q84》热销</h2>
						
						
																											<div id="postmessage_175924" class="t_msgfont">1Q84：村上春树的“超现实”世界 <br />
<br />
日期：2009-07-03 作者：刘柠 来源：文汇报 <br />
<br />
<br />
&nbsp;&nbsp;<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;<br />
<br />
&nbsp; &nbsp; ■刘柠 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp;&nbsp;&nbsp;春夏之交，日本出版界热得有些反常。“长跑者”小说家村上春树，在沉默7年之后，携一部多卷本长篇小说《1Q84》卷土重来，刷新了东洋社会战后出版史纪录：5月29日开始发行，第12天就突破百万。截至6月15日，第一卷售出62万部，第二卷售出54万部。而前两卷合起来，才刚刚描绘到“1Q84”年的上半年。目前，日本所有实体和网上书店均已断货，出版社正紧急增印。据出版商新潮社透露，购读者中，30岁以下的年轻读者过半。受其影响，英国作家乔治・ 奥威尔的社会幻想小说《1984》也开始走俏。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; “1Q84”，顾名思义，既是向奥威尔的《1984》致敬（日文中，Q与 9同音），也有鲁迅《阿Q正传》的投影（村上视鲁迅为东亚现代的起点，不仅写过《阿Q正传》的评论，在短篇小说《没落的王国》中，干脆塑造了一个名叫“Q 先生”的日本精英中产形象）。而创作的出发点，村上坦言，是11年前发生的由奥姆真理教一手制造，造成12人死亡、5510人受伤的东京地铁沙林毒气恐怖案。作者十年如一日旁听了关于奥姆真理教案的几乎所有审判，“不懈地想象那些成为死刑囚的原教徒们的心境”。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 奥姆真理教沙林恐怖案，这个深刻改写了当代日本社会世道人心的事件，也改变了作为小说家的村上的创作道路。在那以前，村上身上的标签是“小资教父”：这个酷爱马拉松、爵士乐和鸡尾酒的“反安保世代”作家，以其作品中四处弥漫的寂寥而冲淡的调子，深刻描绘了发达资本主义国家都市中产阶级子女的彷徨、无奈与内心的挣扎，那种无处不在、刻骨铭心的沦丧感，让何止一代青年自觉、自愿地迷失于“挪威的森林”。其影响所及，早已越出国界，以GDP增长为序，持续哺育着全球化时代跻身中等发达水准的国度和地区的小布尔乔亚分子们：新加坡、香港、台湾、中国大陆、东欧……受事件的刺激，村上于事发当年便推出探讨事件发生机制及其社会背景的长篇纪实作品《地下》及其续篇（分别由讲谈社和文艺春秋社出版）和长篇随笔《时代精神的纪录――关于林郁夫&lt;我和奥姆真理教&gt;》（载《书话》杂志）；继而，又于2000年出版以阪神淡路大地震为背景的小说集《神的孩子都跳舞》（新潮社版），完成了从“小资教父”向关注社会现实的 “硬派”作家的华丽转身。十年弹指，一路下来，村上终于今年3月走向耶路撒冷，一边从权力者手中接过奖杯，一边直视权力者的眼睛，发表了关于“墙与蛋”的著名演说：“……我们都拥有一个真实的、活着的灵魂。体制没有。我们不能让体制来利用我们，不能让体制失去控制。是我们造就了体制而不是相反。”如此决绝的姿态，令那些不分国籍、喝村上乳汁长大成人、被称为“村上的孩子”的小资粉丝们，同样有理由确信：村上老爹的“转型”，将继续引领昔日的小资读者而不是相反。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 就《1Q84》而言，村上的视角是伦理。用他自己的话说，对事件的关涉与介入，意味着从犯罪被害者与加害者的双维视界出发，叩问现代的状况成为可能；同时，反观“我们自己的世代，1960年代后半叶以降，走过了怎样的道路，也不无留下那个时代精神史的意图”。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp;&nbsp;&nbsp;整部小说由二十四章构成，奇数章描绘指尖拥有某种特异功能的女青年“青豆”的故事，偶数章则讲述立志当小说家的文青、预备校（日本考大学前的补习学校）数学教师天吾的故事。青豆沿着首都高速公路紧急非常出口的楼梯走下来，进入到一个与她彼时生活的1984年的现实雷同却不尽相同、有着微妙差异的1Q84的世界；同时，天吾在改写迷一般的少女作者（Fukaeri）的小说《空气蛹》的过程中，被卷进各种奇妙事态，乃至此前始终知足常乐的平常心发生严重的倾斜。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 不仅男女主人公都经历过不幸的童年，小说中的登场人物也大多沦为暴力的牺牲，身心俱裂，创剧痛深。DV、虐童、宗教狂热，暴力以种种名义，贯穿物理空间与人的存在之间的空白地带，有如空气之“蛹”，无孔不入，畅行无阻。这便是主人公存在于斯、挣扎不已的当下――被称为 1Q84的残酷青春。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 与《1984》中无形、但却无处不在的外在支配者“老大哥”（Big Brother）不同的是，君临于1Q84王国的“小人儿”（Little People）可拟人、拟物化存在，“无论是山羊、鲸鱼，还是一粒豌豆，只要构成通道”，它便会现身。而其一旦附体于某种形态，便会带上利己的密码，进而无穷复制，最终支配我们和世界。说白了，1Q84时代的“小人儿”是某种遗传基因。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 对此，青豆发出的绝望质问注定带有明显的存在主义性质：“如果我们只是单纯的遗传基因的运载体的话，我们之中众多的分子为什么还要以复杂、奇妙的形态，漫步人生之长途呢？！” <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; (《1Q84》村上春树著&nbsp;&nbsp;新潮社2009年5月版)</div>

							
							
							
							
