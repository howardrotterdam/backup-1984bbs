&nbsp; &nbsp;&nbsp; &nbsp; <br />
中国独立纪录片导演徐辛的最新作品《克拉玛依》，无意挑战观众的忍耐极限，也不是要呈现一个伟人或一段大历史，只为不平则鸣，说出真相。历史是人民写的。<br />
&nbsp; &nbsp;&nbsp; &nbsp; <br />
本月28日在香港作世界首映的《克拉玛依》，讲述1994年新疆克拉玛依友谊馆大火，一句“学生们不要动，让领导先走”，造成近288名学童葬身火海，官员却都安然无恙。用影片里一个遇难学生家长的话说，事故是“民族的悲剧”。<br />
&nbsp; &nbsp;&nbsp; &nbsp; <br />
特大火灾事故后，政府虽然允许遇难学童的墓碑刻上“因公牺牲”四字，但是打出“稳定压倒一切”的主调，草率埋葬学童，不让学生家长了解事件，阻挠上访，只允许指定媒体报道，更一直不发死亡证，也不办悼念活动。一晃十五年。<br />
&nbsp; &nbsp;&nbsp; &nbsp; <br />
徐辛在2007年底赴克拉玛依，“访问”了数十位遇难学童的家长以及知情人士。把“访问”用引号围著，是因为导演说他基本上没有作访问，而只是告诉家长，他们对著摄像机说话，等于向著无数的观众说话。家长都很愿意说出他们知道的事情，没有审查，没有删剪。用徐辛的话，是一种压抑了十多年的“爆发”。<br />
&nbsp; &nbsp;&nbsp; &nbsp; <br />
徐辛旧事重提，只为让更多的人知道事故的真相。徐辛在放映会后的与观众见面环节表示，2007年跟监制商讨题材，唐山大地震和克拉玛依，选了后者。原因是克拉玛依发生的事件比较接近，另外，1966年在江苏泰州出生的徐辛，曾当老师，现在也是父亲，这些都让他感到与克拉玛依有很近的距离。<br />
&nbsp; &nbsp;&nbsp; &nbsp; <br />
《克拉玛依》最珍贵的部份，是插播了不少当年的新闻报道片段和一些家长拍下的家庭影片。这些都是从家长手里得到的。有观众问，为甚么纪录片只有受害者的声音，没有领导的话语。徐辛表示，他固然不会得到采访领导的机会，也觉得不会因为片子里少了领导而出现不足。“我认为，够了。”徐辛说。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;有观众问，胡锦涛会否看到《克拉玛依》。徐辛表示，他也希望中央领导看到片子，也希望内地人都能看到。<br />
