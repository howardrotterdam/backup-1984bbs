梁思成（ 1901――1972），建筑学家。广东新会人。1927年获美国宾夕法尼亚大学建筑系硕士学位。1928年入美国哈佛大学美术研究院学习。回国后，曾任东北大学教授、建筑系主任，中国营造学社研究员。1947年获美国普林斯顿大学荣誉文学博士学位。建国后，历任清华大学教授、建筑系主任，中国科学院技术科学部委员，中国建筑学会第一至四届副理事长，北京市第一至三届政协副主席。中国民主同盟盟员。1959年加入中国共产党。是第一、二届全国人大代表，第三届全国人大常委，第二届全国政协常委，第三届全国政协委员。长期从事古建筑研究和教学工作。1946年创设清华大学建筑系。对中外古建筑、城市规划、建筑设计造诣颇深。曾参加主持中华人民共和国国徽、天安门广场人民英雄纪念碑的设计。设计了扬州“鉴真和尚纪念堂”。著有《中国建筑史》、《中国雕塑史》、《营造法式注释》、《APictorialHistoryofChineseArchitecture》(英文版)等。<br />
<br />
《全集》主要内容:<br />
<br />
第一卷是２０年代后期和１９３２年等的古建筑调查报告和文章以及《中国雕塑史》等，证实梁先生是研究中国雕塑历史的开拓者。还编入了梁先生早期重要的古建筑调查报告《蓟县独乐寺观音阁山门考》和《宝坻县广济寺三大士殿》、１９３０年印刷发行的、梁思成与张悦（市政专家，早年留学美国哈佛大学系已故建筑大师张兄）合写的《城市设计实用手册――天津特别市物质建设方案》专著、与妻子林徽音合写的著名力作《平郊建筑杂录（上）》。梁先生写的第一篇有关建筑学的文章是用英文写的，题为《一个汉代的三层楼陶制明器》。本卷由傅熹年校阅并注释，刊出了梁思成先生珍贵照片３５帧。<br />
<br />
第二卷编入了１９３３年至１９３５年期间写的古建筑调查报告，由孙大章校阅并注释。其中有：《正定古建筑调查纪略》、与刘敦桢合写的《大同古建筑调查报告》及林徽音与梁思成合写的《晋汾古建筑预查纪略》，鲍鼎、刘敦桢与梁先生合写的《汉代的建筑式样与装饰》。<br />
<br />
第三卷主要内容是１９３５年至１９４６年期间写的文章及调查报告。由杨鸿勋、王世仁校阅注释。此卷除《曲阜孔庙之建筑及其修葺计划》及用英文写的《华北古建筑调查报告》、《中国最古老的木构建筑》及《五座中国古塔》等三篇文章外，还编入了《西南建筑图论》（一―四川部分）和（二――云南部分）。这两篇图论均系未刊稿。四川部分是梁先生与刘敦桢、莫宗江、陈明达于抗战期间，在四川省及原西康省，调查了３１个县市的古建筑、石刻及其他文物，从中选出１０７项，编写而成。第二篇云南部分是１９３８－－１９３９年，刘敦桢、莫宗江、陈明达、刘致平等人在云南调查了１０个县市，选出６８项古建筑、石刻及其他文物，由梁思成据刘敦桢据调查资料编写成的调查报告。这两份调查报告都是弥足珍贵的历史资料。其中不少建筑已成为永诀的建筑。<br />
<br />
第四卷收入了梁先生的重要专著《中国建筑史》、《中国建筑之两部“文法课本” 》、《记五台山佛光寺的建筑》等，由王世仁校阅并注释。其中《中国建筑史》由杨鸿勋校注。此卷还编入了《战区文物保存委员会文物目录》及《全国重要建筑文物简目》两部重要历史文献。前者系抗日战争胜利前夕用中英文两种文字编写的，共收入４００项文物。后者系北平解放前夕应解放军某部之邀编写的，包括全国各地的重要建筑文物。<br />
<br />
第五卷编入１９４５年至１９７１年间写的文章和书信，由左川校阅并注释。这一卷是九卷中较为厚重的一卷，共收入６６篇文章和书信。除编入致中央首长及有关人员的１７封信件外，还编入了《祖国的建筑》等五篇随笔等科普著作。有梁先生与陈占祥合写的《关于中央人民政府行政中心区位置的建议》、《关于北京城墙存废问题的讨论》以及《北京――都市计划的无比杰作》等关于北京城规划与建设的文章和书信。<br />
<br />
第六卷中收入的两部专著《清式营造则例》和由梁思成主编、刘致平编纂的《建筑设计参考图集》都是目前建筑工作者从事古建筑修复及设计所必不可少的参考书，由郭黛校阅并注释。<br />
<br />
第七卷是宋代李诫著《营造法式注释》，由徐伯安、王贵祥校阅注释。《营造法式》这部我国北宋时官订的关于建筑设计和施工的专著到了近代几乎是无人参得懂的 “天书”，经梁先生用去几乎半生的精力，对照古代建筑实例进行研究，终于把它“破译”了，并加以注释，才使今人能够读得懂。<br />
<br />
