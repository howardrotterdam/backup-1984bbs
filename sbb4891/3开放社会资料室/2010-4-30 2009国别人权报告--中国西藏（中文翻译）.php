<strong>西藏</strong><br />
<br />
美国承认西藏自治区和其他省份的藏族自治州、自治县和民族乡为中华人民共和国的一部分。西藏自治区藏族人口约有280万人，自治区以外的藏族人口估计有290万人。政府严格控制关于西藏和其他藏区的信息并限制进入这些地区，因此很难准确判断那里践踏人权的程度。2008年3月的骚乱（译者注：以下译为“08年西藏骚乱”）发生之后，政府加强了对藏区的控制，并将该政策延伸至2009年全年。<br />
【注：March 2008 unrest及以下类似表述（如rioting in March 2008），统一译为“08年西藏骚乱”而非“3&#8226;14事件”，是因为当年的骚乱持续了较长时间，并不仅仅3月14日这一天】<br />
<br />
中国藏区政府的人权记录仍然很差。2008年3月拉萨骚乱和席卷整个青藏高原的骚乱之后，政府大大加强了对言论、宗教、结社和迁徙自由的镇压，并延续至09年全年。当局仍然严重地侵犯人权，包括非法处决、酷刑、任意逮捕、非法拘禁和软禁。西藏特有宗教、文化和语言遗产的保存与发展仍然令人担忧。<br />
<br />
2008年3月，多家寺院的僧尼在拉萨和其他藏族社区举行和平抗议活动，以纪念1959年西藏起义。4天后，抗议活动及相应的安保应对措施发展成为拉萨的藏人骚乱和警察武力镇压。一些抗议者对汉人和回民使用暴力，并在某种情况下造成了他们的死亡。警方随后的行动造成的死亡、受伤、逮捕和侵犯人权的情况不详。2009年，许多藏人，尤其是僧人，因曾参与2008年的抗议和骚乱而被判入狱。<br />
<br />
2009年，相当数量的武警仍然驻守在青藏高原的许多社区。抗议事件的余波持续影响着中国藏区的人权状况。<br />
【注：People's Armed Police (PAP)，武警】<br />
<br />
<br />
<strong>剥夺生命</strong><br />
<br />
许多报告称，政府或政府官员曾任意或非法剥夺公民生命，但是无法独立核实这些报告。没有报告表明官方有调查并惩处那些非法剥夺公民生命的事件。<br />
<br />
1月23日，Pema Tsepag死于当局持续殴打导致的损伤。在这之前，他曾与两名藏族青年于昌都地区左贡县举行抗议活动，呼吁西藏独立、抵制藏历新年。<br />
<br />
根据西藏人权民主中心的消息， 3月25日，公安人员以散发传单号召罢工为由，处死了27岁的Phuntsok Rabten。他是四川省甘孜县Drango寺的僧人。<br />
<br />
3月，Hor Drago 寺的僧人Panchou Lede在藏族农民与士兵的冲突中被打死。当时，农民拒绝承诺保持一定比例用于耕种的土地。据新闻报道，这位喇嘛曾组织农民拒绝耕作。<br />
<br />
据西藏人权民主中心报道，8月，哲蚌寺的32岁僧人Kalden在拉萨的监狱中遭受酷刑后身亡。Kalden于2008年3月被捕，警方并未将其关押地点通知其亲属。<br />
<br />
08年西藏骚乱之后，政府报告说，有22人在拉萨的暴力事件中死亡，其中包括18位平民、1名警官和3名暴徒。然而，外部观察者（包括西藏流亡政府和非政府组织）对于从3月10日开始的官方镇压导致藏区死亡人数的统计各不相同，基本位于100人至218人之间。<br />
<br />
据官方媒体的报道，4月，曾参与08年西藏骚乱的Lobsang Gyaltsen和Loyak被控“纵火致人死亡”而被判处死刑。政府证实他们于10月23日被执行死刑。同时执行的第三例藏人死刑案件的报告未能得到证实。<br />
<br />
<br />
<strong>失踪</strong><br />
<br />
2008年3月的拉萨暴乱之后，当局随意逮捕藏人，包括许多僧尼，其中的许多人仍然不知去向。官方对关押人数的统计并不完善，只包括很有限的地区。2009年2月10日，官方媒体报道， 953人于暴乱事件后在拉萨被拘留或自首。报道说，76人因与骚乱事件有关被判入狱，另外116人仍在等候审判。<br />
<br />
根据国际援助西藏活动组织的消息，西北民族大学学生Tashi Rabten发表新书《血书（Written in Blood）》后不久，于7月失踪。Tashi Rabten曾编辑了一本被禁的谈论08年西藏骚乱的文集。<br />
【注：International Campaign for Tibet，中译为“国际声援西藏运动”或“国际西藏运动”，非盈利、非政府的人权组织，总部位于美国华盛顿】<br />
<br />
纪录片制片人Dhondup Wangchen仍然身处青海省西宁市附近一所未知的监狱里。当局强迫他解聘前一位来自北京的辩护律师，并通知他的家人只有青海省的律师才可以代理他的案件。青海当局拒绝了外国外交官旁听审判的请求。2009年12月28日，青海省的一个法庭以拍摄电影批评西藏人权状况为由，判处Dhondup Wangchen有期徒刑6年。直至年底，其服刑地仍无人知晓。<br />
<br />
5位僧人下落不明，包括Sonam Rabgyal、 Damdul和Rabgyal。2008年4月的一次午夜突袭搜查之后，他们3人从拉萨的小昭寺失踪了。藏族传统绘画画家Paljor Norbu在2008年11月的一次秘密审判之后被判处有期徒刑7年，直至年底仍然不知所踪。班戈县普布镇副镇长Phuntsok Gyaltse（平措坚赞）于2007年4月被捕后仍然没有新的消息，至今下落不明。<br />
【注：班戈县通常英译为Baingoin County，也有人写作Palgon County】<br />
<br />
