2009年1月，拉萨市公安局开展了全市范围的“严打”运动。尽管这表面上是一场反犯罪行动，但警方却在私人住宅、客房、旅馆、酒吧和网吧搜查达赖喇嘛的照片和其他政治违禁物。警方通过检查拉萨居民的手机以搜寻来自印度的“反动音乐”和达赖喇嘛的照片。根据一家外国报纸报道，超过5000名嫌疑人被搜查，至少81人被拘留。人权团体认为，“严打”运动的动机其实是骚扰人权活动家和藏独支持者。<br />
<br />
鉴于08年西藏骚乱出现了死亡事件，国内外许多藏人主张藏历新年（今年是2月25日）成为一个追悼和祈祷的日子，而非进行庆祝。为了抵消藏历新年的杯葛行动，许多藏区官员要求藏人庆祝这个节日。在藏区的一些地方，当局向政府办公室和工作单位发放烟花炮竹，并要求工人参与庆祝活动。官方媒体大篇幅报道了藏历新年的庆祝活动。青海省贵南县Lutsang寺的一百多位僧人在藏历新年举行了一场烛光守夜活动，并向县政府总部和平游行。他们全部被逮捕，除6人之外，几周后又都被释放。2009年4月，其中的4名僧人被判处2年有期徒刑。<br />
<br />
2009年3月28日，西藏自治区开始庆祝一个新创造的节日――“农奴解放日”，以纪念在1959年的这一天，中国的统治者正式废除达赖喇嘛地方政府。政府精心策划的庆祝活动包括在布达拉宫广场举办的大型庆祝仪式和一台电视音乐会。在这个新节日的筹备阶段，官方媒体发动了新一轮针对达赖喇嘛的批判。节日之前公布的一份政府白皮书中说，达赖喇嘛家曾有6000名奴隶，国家解放西藏农奴 “完全可以与美国南北战争解放黑奴相提并论” 。<br />
<br />
达赖喇嘛及其他观察者都担心发展项目和其他中央政策会使非藏族居民享受绝大部分利益，并持续推动汉人、回民和其他民族人口大量涌入西藏。2009年11月24日，中国政府报道，青藏铁路自2006年开通以来，已运送830万旅客和62.21公吨货物。<br />
<br />
当地居民无权参与保护自己的文化遗产和自然环境。2007年西藏政府修订了《西藏自治区文物保护条例》，坚持对宗教遗迹和寺院的所有权。<br />
<br />
在西藏自治区，藏语和汉语（普通话）均为官方语言，公共标识和商业招牌上也使用两种语言。汉语使用广泛，正式交流大都使用汉语。2000年的人口普查数据显示，藏族文盲率（47.6%）是全国平均水平（9.1%）的5倍多。许多农村和游牧地区的儿童在进入汉语学校学习前，只接受了1至3年的藏语教育。官方数据显示，2008年，青年和达到工作年龄成年人的文盲率从2003年的30.9%下降到2008年的2.4%。然而，这一群体的文盲率在有些地区要高得多。根据新华社2006年的一则报道，相对于西藏农村地区说普通话的居民来说，藏语居民识字率的定义要宽泛得多。如果说藏语的农民和牧民能读写30个藏语音节字母，就可以认定为识字。说普通话的牧民认识1500个汉字，才能认定为识字。<br />
<br />
政府已经建立了全面的全国藏语教学课程，藏区许多小学都将藏语作为主要授课语言。藏族学生必须学习汉语，算术和科学等课程一般使用汉语教学。在初中和高中（即使是一些官方指定的藏语学校），教师往往也只在藏语、文学和文化课中用藏语教学，其他所有课程都用汉语讲授。<br />
<br />
从实际上看，掌握流利的汉语是接受高等教育的基本条件。中国最著名的大学不提供藏语或其他少数民族语言的教学。面向少数民族学生的稍低一等的大学只在教授侧重于藏语或藏族文化研究的课程时使用藏语。在少数民族大学里，藏族和其他少数民族学生一般都精通汉语，因为许多课程（如计算机和商业贸易课）都是用汉语讲授的。<br />
<br />
中国主要的大学在入学考试时一般要求对英语有一定掌握，但大多数西藏学校毕业生只学习了普通话和藏语，因而无法进入更好的大学学习。这导致藏族缺少接受过自然科学和工程科学培训的人，因此西藏的开发项目几乎完全依赖从其他地区引进技术专家。<br />
<br />
<br />
<strong>宗教自由</strong><br />
<br />
虽然法律规定了宗教信仰自由，但宗教自由的实际水平仍然很低。2009年，政府继续紧密控制着藏传佛教的教学与实践；同一年，中国共产党持续努力抹黑宗教领袖达赖喇嘛，并将对他的尊重与在政治上反对政府和中共联系起来。<br />
<br />
新闻和非政府组织的报告表明，藏区政府对宗教活动和宗教场所继续严厉管制，再加上社会与经济因素，就是激化人与人之间怨恨的主要原因，并导致了08年西藏骚乱。虽然当局允许开展多种传统宗教活动，也允许公民公开表明自己的信仰，但是对于那些他们认为是用来表达不同政见或鼓吹西藏独立的活动（包括公开崇拜达赖喇嘛），当局会迅速而有力地进行镇压。藏区的政府官员把佛教寺院和支持独立的行动主义紧密联系在一起。<br />
<br />
2009年，当局关闭了藏区多座寺院，拘捕了数目不详的僧尼，并虐待他们的身体，或者将其逐出寺院。2008年，超过500名来自西藏以外藏区的僧人被逐出拉萨的寺院。直至今年年底，他们仍然未被允许返回拉萨。在藏区的一些地区，当地公安局在寺院内安装监视器、建立派出所，以监视僧人的一举一动。<br />
<br />
据西藏人权民主中心的消息，2009年7月24日，青海省Jachung 寺僧人首领Lobsang Tsultrim被寺院开除，并被禁止加入其他寺院。此前，官员要求他去召集其他僧人参加“爱国主义教育”活动，但没有一位僧人出现。Lobsang Tsultrim因抵制“爱国主义教育”运动而被指责。<br />
<br />
08年西藏骚乱之后，当局强迫僧人每周，有时每天参加政治教育活动。这一政策在2009年继续执行，尽管其频率和强度有所减弱。在这一年里，“爱国教育”和“法制教育”计划仍然在寺院机构、工作场所、企业和学校进行。一些地区的政治教育活动包括强制声讨达赖喇嘛。官员还强迫僧人将达赖喇嘛的画像从经堂和私人住所移除，但各地区对该命令的执行力度明显不同。对著名大寺院宗教活动的限制最严格，比如拉萨的哲蚌寺和色拉寺，甘孜州（的..寺），四川的格尔登寺，甘肃省夏河县的拉卜楞寺，青海省西宁附近的塔尔寺。<br />
【注：原文缺少甘孜州的寺院名称】<br />
<br />
在西藏自治区和其他藏区，每逢达赖喇嘛的生日、敏感日周年（如1959年西藏3月的西藏起义）和节庆时期，政府都会加强安保措施。当局仍然禁止庆祝达赖喇嘛7月6日的诞辰。<br />
<br />
政府仍禁止根敦&#8226;确吉尼玛的肖像，他是达赖喇嘛承认的班禅喇嘛。“官方”班禅喇嘛坚赞诺布的画像并不多见，只有一些在政府严密控制下的著名大寺院才摆放，而且只在政府官员要求的情况下摆放。但前任班禅喇嘛、他的女儿以及噶玛巴喇嘛（藏传佛教噶玛噶举派领袖，藏传佛教中最有影响力的宗教领袖之一，于1999年逃往印度）的照片在多处有售，且随处可见。藏传佛教僧尼拥有和展示达赖喇嘛肖像的能力根据地区的不同而有很大差异。一般农村的寺庙里很少有汉族旅游者和官员，因此得以展示达赖喇嘛的肖像。在一些寺院，僧人可以在自己的私人住所展示达赖喇嘛的肖像，尽管这些图像通常不允许在公众区域展示。<br />
<br />
政府限制汉族佛教徒在西藏自治区和其他藏区的寺院内生活、学习。西藏自治区之外的僧侣若想赴西藏自治区研修，必须获得本省宗教事务局和西藏自治区或其他藏区的正式许可，但这种许可很难获得。<br />
<br />
虽然藏族僧侣不得在藏区以外举办大规模的宗教讲学，但许多僧侣仍在中国非藏区私自传教。据报道，当局有时会阻碍西藏以外地区的汉族佛教徒邀请藏族僧侣讲学。这些活动需要得到僧侣所在地和接收省份宗教事务局的明确许可。尽管如此，藏族僧侣有时会穿便服到藏区以外的地区传教。<br />
<br />
西藏自治区的寺院和其他藏区的主要寺院不得与其他寺院建立任何关系，或联合举办宗教活动。其中一个事例是，四川省马尔康县当局一再拒绝给予Tsodham寺举办年度宗教活动的许可。这项活动原本定于2010年初举行，届时将会有来自西藏自治区的康巴和安多地区五十多座寺院的僧侣汇聚一堂。<br />
【注：马尔康县位于四川省阿坝州，而非西藏自治区。原文有误】<br />
<br />
政府继续资助宗教和文化遗址的修复工作，并将其作为藏区旅游业发展计划的一部分。许多藏人担心，促进寺院的旅游业会影响僧人的宗教活动，使其无法集中精神。<br />
<br />
详细讨论请登陆<a href="http://www.state.gov/g/drl/irf/" target="_blank">www.state.gov/g/drl/irf/</a> ，见《2009年国际宗教自由报告》。<br />
<br />
<br />
<strong>迁徙自由</strong><br />
<br />
法律规定了旅行自由，但在实践中，政府严格控制藏人的旅行和迁徙自由。<br />
<br />
在拉萨、整个西藏自治区和青海、甘肃和四川省的藏区，迁徙自由都受到了严格限制，尤其是对僧尼来说。武警和当地公共安全局在主干道上、城市里和寺院周边设置了多处路障和检查站。身着宗教服饰的藏人在旅行中会受到检查站警察额外仔细的检查。一些藏族僧人报告说，在他们的本寺之外旅行仍然很困难，官方经常拒绝许可外地僧人在某处寺院为接受宗教教育做短暂停留。<br />
<br />
许多藏人（尤其是知名宗教人士、学者、异议人士和来自农村地区的藏人）仍然报告说，很难获得护照。在08年西藏骚乱后，藏人获得护照变得更加困难，而且也很难续签。某些情况下，藏人被迫承诺不前往印度才能获取护照。某些情况下，获得外国大学奖学金的藏族学生因当局拒绝发放护照而无法出国留学。<br />
<br />
藏族人为了宗教、教育和其他其他目的前往印度仍然面临很大的困难和障碍。西藏自治区和四川甘孜州的党政干部不得送孩子出国留学。除了护照的限制之外，强化边境管控也使得旅行（如前往尼泊尔和印度朝圣，拜见达赖喇嘛）更加困难。<br />
<br />
在敏感事件周年期间，政府会限制藏人的活动和迁徙，同时加强周边地区的管控。有随意拘捕民众的报道，尤其是从印度和尼泊尔返回的僧尼。拘留通常持续数月，但当局并未正式起诉被囚禁者。<br />
<br />
藏区边境管控严密，以限制人员穿越边境前往尼泊尔和印度。2009年，印度达兰萨拉的西藏难民接待中心接待了838名来访者。这一数字较2008年有所增长，但与往年相比却明显下降。<br />
【注：The Tibet Reception Center，中译为“西藏难民接待中心”】<br />
<br />
达赖喇嘛、噶玛巴喇嘛和其他藏传佛教派别的领导人仍然处在流亡状态。<br />
<br />
政府还管制进入西藏自治区的外国旅游。根据1989年的一项规定，外国游客进入西藏自治区之前都必须获得中国政府的官方确认函。大多数游客通过向官方注册的旅行社预定旅行来获得这种证明信。08年西藏骚乱爆发后的几个月内，当局几乎终止了所有前往西藏的外国旅游。2009年3月，正值1959年西藏起义50周年，外国游客又一次被禁止前往西藏。3月之后，前往西藏旅游的外国游客人数持续增长，但是当局执行了比之前更加严格的规定，外国游客必须组团。<br />
<br />
获官方许可再次前往拉萨旅游的外国公民，其活动范围被严格限定在城市及周边地区。中国官员仍然严格限制外交官和记者前往西藏。外国官员和记者只能在西藏外事办公室安排的严密陪同下前往该地区。外国外交官每次前往西藏都必须获得西藏自治区外事办公室的许可，而且很难获得。<br />
<br />
对西藏自治区的官方访问受到严密监视。代表团成员很少有机会接触到未经当局事先安排的原住民。除了几个被严密控制的旅行之外，有关部门多次拒绝国际观察员前往藏区评估局势。<br />
<br />
<br />
<strong>少数民族</strong><br />
<br />
尽管西藏自治区的人口普查数据表明，藏族人占西藏常住人口的92%，但官方数据不包括大量长期、中期和短期居住的汉族居民，如干部、技术工人、非熟练工人、军队和准军事部队及其家属。中国社会科学家估计，在5月至11月旅游旺季和外来务工高峰期，仅在拉萨，流动人口（包括观光客和短期来藏商务旅行）的数量就超过20万（这一数字占拉萨总人口的一半，超过西藏总人口的10%）。<br />
<br />
迁移到西藏的居民绝大多数集中在城镇。在那里，政府的经济政策不成比例地有利于汉人。汉人和回民经营的小买卖（大多是餐馆和零售店）在整个藏区城市中占据优势地位。根据官方人口普查数据，近98%的农村人口是藏族人。<br />
<br />
政府在整个西藏自治区和其他藏区继续开展着将藏族牧民重新安置到城镇地区的运动。当局用金钱奖励的方法鼓励牧民宰杀或出售牲畜，然后搬入新建的藏族社区。但有报道称，曾经许诺的对强制重新安置的补偿范围，要么没有兑现，要么未足额兑现。<br />
<br />
2007年1月，西藏自治区党委书记张庆黎称，重新改造西藏畜牧业社区不仅是为了促进经济发展，还是为了抵消达赖喇嘛的影响。他还指出，这一举措对“继续实施西部大开发战略”至关重要。据新华社2009年3月20日的报道，西藏自治区共有二十一万九千八百户牧民。截至2008年底，其中的二十万户（即一百万名农牧民）已经被安置入住永久性住宅。<br />
<br />
改善西藏最贫困人口的住房条件和教育是重新安置计划的目标之一，但有规定要求村民在两到三年内严格按照官方规范建造房屋，这迫使被重新安置的家庭借债支付修建费用。<br />
<br />
2008年，一家官方媒体报道，藏族和其他少数民族占西藏政府职员人数的69%，但几乎所有的县和自治州，中国共产党的最高职务仍由汉族人担任，包括西藏自治区党委书记。在政府任职的藏族官员不得前往寺院礼拜或信奉宗教。<br />
<br />
藏人在经济和社会方面受到排斥，是各行各业的藏人（包括经营者、工人、学生、大学毕业生、农民、牧民）参与08年西藏骚乱的主要原因。有些藏族人反映在就业中受到歧视，一些藏区的招聘广告注明藏人请勿申请。一些人声称汉人被优先雇用，从事同一工作的汉人报酬更高。还有藏族人反映，藏人比汉人更难得到开办企业的许可和贷款。城市地区广泛使用汉语，不说汉语的藏族人在许多企业的就业机会将受到限制。向藏族社区提供援助的国际非政府组织受到了新的限制，这导致许多非政府组织项目的消失，许多外国非政府组织员工被驱逐出西藏自治区。<br />
<br />
西藏自治区旅游局仍坚持其政策，拒绝雇用在印度或尼泊尔受过教育的藏族导游。政府官员表示，所有在西藏自治区工作的导游都必须在旅游局寻求职位，并必须通过旅游证书和政治意识形态考试才能取得导游证。政府声称，这样做的目的是为了确保所有的导游能向游客表达政府反对西藏独立和反对达赖喇嘛活动的立场。西藏自治区的一些藏族导游报怨，他们面临着政府资助的“援藏”导游的不公平竞争。这些导游来自其他地区，只需接受有关西藏知识的速成课程就能立即上岗。<br />
<br />
<br />
<strong>妇女和儿童</strong><br />
<br />
对妇女参政没有形式上的限制，妇女担任了许多级别较低的政府职位。但是，在省、自治州一级政府部门中任职的妇女人数较少。根据官方网站的信息，西藏的妇女干部占整个自治区干部总数的30%以上。<br />
<br />
没有关于强奸和家庭暴力的信息。<br />
<br />
藏区的卖淫问题日益严重，拉萨地区经营着数百家半公开的妓院。西藏自治区的国际开发人员报告说，目前还没有可靠数据能反映西藏地区最大的两座城市（拉萨和日喀则）内从事商业性交易的人数。一些卖淫活动发生在共产党、政府或军队所属场所内。西藏的妓女大都是汉族妇女，主要来自四川省。但也有藏人卖淫，她们主要是来自农村或游牧地区的年轻女子。虽然还不清楚藏区卖淫者感染艾滋病毒/艾滋病的几率，但西藏自治区卫生局报告说，1993年至2009年，西藏自治区共发现102例艾滋病毒/艾滋病感染，其中包括2009年内1月到11月新增的28例。由于不了解艾滋病毒传播知识以及经济上的压力，卖淫女在性行为中并未采取保护措施。<br />
<br />
较之汉人，计划生育政策允许藏人和其他少数民族生育更多的孩子。拥有固定工作的藏族城市居民、共产党员和政府官员，以及生活在藏区的汉人，通常可以生两个孩子。政策鼓励农村地区藏人最多生育三个孩子，但不作硬性规定。<br />
<br />
政府政策规定普遍开展免费义务初等教育。据西藏自治区官方统计，96.5%的6至13岁儿童在校就读，西藏自治区52万小学生中有90%完成了初中教育，完整地接受了九年义务教育。2003年，联合国教育权利问题特别报告员报告说，中国官方教育统计数据未能准确地反映入学率，也没有得到独立的核实。<br />
<br />
西藏自治区是中国少数几个不存在因选择性堕胎和对女婴照料不足而导致性别失衡的地区之一。<br />
