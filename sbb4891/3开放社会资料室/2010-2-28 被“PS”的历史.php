													<h2>被“PS”的历史</h2>
						
						
																											<div id="postmessage_496595" class="t_msgfont">　　<strong><font color="#000080">&nbsp;&nbsp;谁控制了历史，谁就控制了将来；谁控制了现在，谁就控制了历史。<br />
<br />
　　</font>&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; </strong><br />
<br />
　　<strong>&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;――奥威尔《一九八四》</strong><br />
<br />
　　熟稔历史的人都知道，图片包含着文字无可替代的信息，比文字更具有震撼力和冲击力，是传播新闻、记录历史的重要载体。然而，在“政治正确”的名义下对历史图片进行加工、修改籍以达到某种目的的现象早已司空见惯。这种歌功颂德、排斥异己、掩盖真实的文饰、删改，是专制和愚昧的产物，不仅有悖于最起码的新闻原则，而且与现代文明的要求背道而驰。这是对历史的粗暴阉割和肆意篡改，也是对公众赤裸裸的蒙蔽和欺骗。<br />
<br />
　　艺术家张大力先生心怀对历史的敬畏，甘于寂寞，从浩如烟海的资料中搜集了一些为大家所熟悉的图片和底片，通过比对，还原了它们的最初面目。近些年来，这些图片以“第二历史”的名义在全国各地展出，引起了广泛的共鸣和深刻的反思。<br />
<br />
　　这些被“PS”的图片，大体上可分以下几种情况：<br />
<br />
　　<strong><font color="#000000">一、主人公由于遭受批判，被划入另类</font></strong><br />
<br />
　　<img src="http://img.blshe.com/resserver.php?blogId=965&amp;resource=2139880-image001.jpg&amp;mode=medium" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　<strong><font color="#800000">图1&nbsp;&nbsp;1964年，毛泽东、刘少奇、朱德、欢迎周恩来从苏联归来</font></strong><br />
<br />
　　<strong><font color="#800000"></font></strong><br />
<br />
　　上个世纪七十年代，中国农村还相当贫困，那时候，许多家庭哪怕再穷，春节时也要在家里贴上几幅年画。图1上面部分的照片，就是七十年代中国年画中的重要作品。就是这幅年画，在我家布满烟灰的黝黑的墙上贴了好几年。然而，谁能料想，到机场欢迎周恩来回来的不但有毛泽东、朱德，而且还有刘少奇。只是后来刘少奇被以“叛徒、内奸、工贼”的罪名打入冷宫后，艺术家们只得让他从伟人的行列中消失，以示彻底决裂。少不更事的我不可能想许多，食不果腹的农民们更无心思去考虑这些，他们受用的只是色彩带来的喜气而已。<br />
<br />
　　<img src="http://img.blshe.com/resserver.php?blogId=965&amp;resource=2139881-image003.jpg&amp;mode=medium" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　<strong><font color="#800000">图2&nbsp;&nbsp;1959年，毛泽东和刘少奇在第二届全国人民代表大会上</font></strong><br />
<br />
　　<img src="http://img.blshe.com/resserver.php?blogId=965&amp;resource=2139882-image004.jpg&amp;mode=medium" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　<font color="#800000"><strong>图3&nbsp;&nbsp;1933年，宋庆龄、鲁迅等欢迎萧伯纳</strong></font><br />
<br />
　　非独政治领域存在着你死我活的争斗，在文学和艺术领域也不例外。当鲁迅一跃而成为“中国的脊梁”和“民族魂”之后，自然就不能和资产阶级文人林语堂携手并肩了。<br />
<br />
　　这张照片是1933年2月17日，鲁迅、蔡元培、宋庆龄、史沫特莱、罗伊生、林语堂在上海欢迎萧伯纳时的照片，在解放前并没有出版。1951年9月上海《文艺新地》刊出时，就剩下五人了，林语堂和罗伊生被抹去。自此这张照片广为流传。<br />
<br />
　　林语堂1926年任厦门大学文科主任，由于他的推荐和邀请，鲁迅到厦门大学执教。后来和鲁迅由于“人性”的观点相左而失和；加之林语堂曾经就读教会大学、留学德国、定居美国、用英文写作，都被看成是“买办行为”。这样一个“买办文人”怎么可以与伟大的鲁迅出现在同一张照片上呢？而美国人伊罗生呢?虽然他曾在上海《大陆报》工作，和鲁迅、茅盾相交甚好，但他的思想和一些行为与托派有关联。因此，将他从鲁迅等人的合影中剪去，是理所当然的了。 <br />
<br />
　　<font color="#000000"><strong>二、由于政治生态的变化，对旧有的历史进行修正</strong></font><font color="#6b2335"> </font><br />
<br />
　　<font color="#6b2335"><img src="http://img.blshe.com/resserver.php?blogId=965&amp;resource=2139883-image005.jpg&amp;mode=medium" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　<strong></font></strong><br />
<br />
　　<font color="#6b2335"></font><br />
<br />
　　<strong><font color="#800000">图4&nbsp;&nbsp;1945年，毛泽东和朱德在一起</font></strong><br />
<br />
　　1937年，卢沟桥事变爆发后，国共两党同仇敌忾，共同抵抗日本军国主义的入侵。1937年8月，朱德以国民革命军第八路军总司令的名义率部东渡黄河，开赴抗日前线。在抗日战争取得决定性胜利的前夜，具有重大历史意义的中共七大在延安召开，期间，著名的红色色影视摄影师徐肖冰拍下了图4上这幅照片。<br />
<br />
　　谁能料想抗战烽烟甫歇，国共纷争又起。在这样的情势下，一味宣传国共合作似乎有些“不合时宜”，朱德帽子上那颗刺眼的“青天白日”徽章便识趣地消失了，真是一叶枯而知天下秋，瓶水冰而知冬日寒！<br />
<br />
　　<img src="http://img.blshe.com/resserver.php?blogId=965&amp;resource=2139884-image007.jpg&amp;mode=medium" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　<img src="http://img.blshe.com/resserver.php?blogId=965&amp;resource=2139885-image009.jpg&amp;mode=medium" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　<strong><font color="#800000">图5&nbsp;&nbsp;1972年2月21日，周恩来迎接尼克松</font></strong><br />
<br />
　　在颠簸飘摇的波涛中，非但大人物的命运波澜起伏，就连小人物也捉摸不定。1972年2月21日，美国总统尼克松访问中国，冀朝铸作为周恩来的翻译参加了接见。在尼克松走下舷梯的时候，周恩来特意叮嘱冀朝铸：“近一点，每一句话都得认真听清楚，准确地翻译”。于是了尼克松这句传世名言“我是跨过太平洋与中国人民握手”通过冀朝铸的口中走向了世界。然而，在官方宣传报道的图片中，冀朝铸却无影无踪（图5上），出现在周恩来身后的却是几乎不懂英语的翻译王海容。而尼克松的女儿朱莉寄给冀朝铸的签名照（图5下）却真实地再现了当时的场景。至于冀朝铸“被消失”的原因，几乎无人知晓。我不揣冒昧猜测大约是他太高大了的缘故吧。<br />
<br />
　　<strong>三、为了突出主要人物的形象而进行加工</strong><br />
<br />
　　在被“PS”的图片中，这类图片占相当大的比重。突出主要人物、突出英雄人物一直是我们舆论宣传的指针，在这一思想的指导下，历史图片被无情的再加工到令人触目惊心的程度。<br />
<br />
　　<img src="http://img.blshe.com/resserver.php?blogId=965&amp;resource=2139886-image011.jpg&amp;mode=medium" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　<strong><font color="#800000">图6&nbsp;&nbsp;1958年毛泽东、彭真等和群众一起参加十三陵水库劳动</font></strong><br />
<br />
　　1958年，毛泽东在时任北京市长彭真的陪同下到北京十三陵水库参加义务劳动。图6右毛泽东左边的就是彭真。而在广而告之的宣传画中，彭真早已无影无踪了（图6左）。<br />
<br />
　　<img src="http://img.blshe.com/resserver.php?blogId=965&amp;resource=2139887-image012.jpg&amp;mode=medium" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　<strong><font color="#800000">图7&nbsp;&nbsp;1944年，毛泽东在朱德、王震的陪同下检阅359旅</font></strong><br />
<br />
　　真实的事件只有一个版本，而被“PS”的历史却可以有无数不同的声音。同一个时间、同一个地点、同一片土地、同样的一群人，为什么昨天的报道就和今天的图片轩轾如此呢？真相，因为追问而显现；历史，因为探究而真实。<br />
<br />
　　<img src="http://img.blshe.com/resserver.php?blogId=965&amp;resource=2139888-image013.jpg&amp;mode=medium" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　<strong><font color="#800000"></font></strong><br />
<br />
　　<strong><font color="#800000">图8&nbsp;&nbsp;1974年，邓小平第一次复出</font></strong><br />
<br />
　　在图片师巧夺天工的修剪下，毛泽东和邓小平的形象得以突出。可仅凭修改后的图片，我们已经很难解读出这次见面的背景了。如果图片不能传递出真实的信息，那这样的图片也就失去了其应有的历史价值。<br />
<br />
　　<font color="#000000"><strong>四、为了图面的美观进行“再创作”</strong></font><br />
<br />
　　<img src="http://img.blshe.com/resserver.php?blogId=965&amp;resource=2139889-image014.jpg&amp;mode=medium" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　<strong><font color="#800000"></font></strong><br />
<br />
　　<strong><font color="#800000">图9&nbsp;&nbsp;雷锋的标准像</font></strong><br />
<br />
　　青松苍翠、白云朵朵映衬下的雷锋光彩照人。与右边的图片相比，图9左的图片不仅更美观，而且更富有精神层面的隐喻和暗示。但当你知道左边这个图片是人工修饰的时候，你还会如此欣喜吗？<br />
<br />
　　原来，右边这张照片是《民兵之友》记者周军在沈阳军区政治部大院拍的。1977年，上海人民美术出版社为了出版《雷锋画册》的需要，采用了一种叫“印染法”的技术将一棵黄山松植入照片，形成了左边这幅照片。<br />
<br />
　　<img src="http://img.blshe.com/resserver.php?blogId=965&amp;resource=2139890-image015.jpg&amp;mode=medium" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　<strong><font color="#800000">图10&nbsp;&nbsp;1952年，毛泽东、朱德、周恩来在第一届全军运动会上</font></strong><br />
<br />
　　这又是一张被进行了“手术”的照片。很显然，修改后的照片无论在人物形象、环境展示、政治方向还是在视觉效果方面都胜出一筹。但面对这样的照片，我们除了佩服修改者的高超手艺之外，还能说些什么呢？<br />
<br />
　　张大力把这些修改过的照片构成的历史称做“第二历史”，“我们目前看到的和知道的，都是第二历史，第一历史被有意或无意地遮蔽了。”当后人试图从这些被“PS”过的图片中去解读历史的时候，他们得到的往往是被扭曲的“真相”和不对称的信息，这被“遮蔽的历史”与其说是历史，毋宁说是艺术家笔下的作品更符合实际。<br />
<br />
　　昨天的新闻就是今天的历史。一件件往事就像涓涓清流汇入奔腾不息的历史长河。当心存温情向历史致意时，我们的崇敬也应得到最起码的尊重吧！</div>

							
							
							
							
