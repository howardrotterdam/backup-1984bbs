													<h2>一个唱跨了政权的BAND</h2>
						
						
																											<div id="postmessage_217609" class="t_msgfont">节选自马世芳《地下蓝调乡愁》：<br />
　　<br />
　　<br />
　　一个唱垮了政权的摇滚乐团<br />
　　――“宇宙塑料人”的传奇故事<br />
　　<br />
　　<br />
　　……<br />
<br />
　　哈维尔是在一九七六年，他四十岁的时候迷上摇滚的。一个大雪纷飞的夜晚， 有个朋友拎着酒跑来敲他的门，跟他彻夜长聊，并且还提议他跟一个名叫伊凡・希罗斯（Ivan Jirous）的年轻人见见面。他跟哈维尔说，希罗斯别名“马哥”（Magor）――这个字在捷文的意思是“疯汉”。马哥不但是“宇宙塑料人”这个乐团的“艺术总监”，还有一群满怀激情的波希米亚浪子，把马哥当成精神领袖，他们替这个次文化社群取名为“地下社会”。雪夜来客说：他们真精彩，你真该认识一下这群小伙子。<br />
　　<br />
<br />
　　关于马哥这号传奇人物，有位记者是这么说的：“马哥经历过嗑药、酗酒、摇滚、吃牢饭、被条子痛揍、样板审判、重刑监狱、神话传奇、一场大革命，还有很多、很多、很多的诗。”经过引荐，哈维尔抱着“姑妄听之”的心情，和他在布拉格相会。马哥是个长发披肩、形容邋遢的汉子，一面滔滔不绝发表他对捷克音乐复兴的看法，一面拿出两三卷卡带，塞进录音机，放给中年剧作家听，那是几个当地摇滚乐团，包括“宇宙塑料人”的表演实况。哈维尔听着破录音机里的音乐，大受震动，于是推掉了其他约会，跟马哥跑去酒吧，彻夜聊到天明，从此成为忘年至交。 <br />
<br />
　　哈维尔在多年后回忆那天的感觉：“这种音乐有一种震撼人心的、使人不安的魔力，这是一种使人警醒的、由内心深处发出的真诚的生命体验，任何人只要精神尚未完全麻木，就能理解……我突然领悟到，不管这些人的语言多么粗俗，头发多么长，但真理在他们这边。” <br />
从这天开始，哈维尔变成了“宇宙塑料人”的忠实歌迷。后来，这些年轻人又介绍他听地下丝绒和弗兰克・扎帕的音乐。它们从一张张刮花了的旧唱片，转拷成一卷卷秘密流传的卡带――在那个年头，一旦被秘密警察发现你在听美国摇滚乐，可是会被抓去关的。后来，就跟千千万万捷克青年一样，不知不觉，哈维尔已经把摇滚乐视为和文学一样重要的生命元素了。 <br />
<br />
　　在肃杀、灰暗的七○年代，许多捷克政治犯被秘密警察逮捕，关押在牢房里。面对无穷无尽的审讯与折磨，他们重获平静的方法，往往不是祈祷，而是轻轻哼唱娄・里德的歌、背诵约翰・列侬和鲍勃・迪伦的诗句。弗兰克・扎帕和“地下丝绒”的唱片，在美国从来就不是畅销品，然而扎帕怪异突梯、充满荒谬色彩的音乐，和“地下丝绒”描述种种陷溺堕落景象的作品，对捷克青年来说，毋宁是更贴近生活实况的。 <br />
<br />
　　在一九六八年苏军坦克大举压境之前，布拉格生气蓬勃的文化圈，经常被拿来跟六○年代的“摇摆伦敦”（Swinging London）和纽约东村相提并论：大家写诗、蓄长发、搞现代艺术、弹吉他、嗑药、穿五颜六色的衣服，觉得生活理应就是这样。也不知该说幸或不幸，“宇宙塑料人”的成立，正巧在“布拉格之春”被十八万大军剿灭之后一个月，碰上了当权者用尽全力要铲除那种花花绿绿的生活，让一切回归“正常化”的起点。一九七一年，官报公然宣示：“政府不会容许‘百花齐放’，我们所要栽培、要灌溉、要保护的，只能是那唯一的花朵――马克思主义的红蔷薇！”“宇宙塑料人”的披肩长发、奇装异服、放荡行径和高分贝的摇滚乐，简直摆明了跟国家机器过不去，注定了他们被整肃的命运。 <br />
　　<br />
<br />
　　读到这里，你一定会猜想，“宇宙塑料人”应该是个政治意识十分强烈的乐团吧。但肉店学徒出身的团长米兰・贺拉夫萨（Milan Hlavsa）回忆说：才不是哩，他只是忍不住想玩摇滚而已。“宇宙塑料人”的歌词完全不提政治，甚至连抱怨与哀叹的情绪都不多见。他们是这样相信的：“对这个荒谬体制最好的反击，就是竭尽所能地忽视它。”他们用不和谐的高分贝噪音、粗鄙的打油诗、油腻的长发、惊世骇俗的打扮和浪荡的生活方式，直接把官方标举的那套“好公民的价值”扔进了茅坑。一位捷克文化人这样描述“宇宙塑料人”的行径：“他们不跟当政者对话，只跟自己人对话；他们没有变成异议分子，反而创造出一种可以暂时满足自己的另类文化；他们没有要求当权者赏给自己更多的自由，相反地，他们的行径就好像自己已经拥有了自由一样。” <br />
　　<br />
<br />
　　不消说，这替他们惹来了无穷无尽的麻烦。 <br />
<br />
　　在七○年代初“正常化”运动雷厉风行之下，无论搞摇滚的小伙子多么不愿意和政治扯上关系，都不可能幸免。所有乐团都必须重新接受“资格审查”，领有执照才能表演。团名或歌词不能出现英文、不能蓄长发、不能穿奇装异服、不能写晦暗悲观的歌词、不能有挑逗夸张的舞台动作、不能把音量开得太大声……最糟糕的是，万一没领到执照，先不说你根本没有场地可以表演、拿不到演出酬劳，连舞台音控器材都会被警察没收，因为那是国有财产。<br />
　　<br />
　　以上各项标准，“宇宙塑料人”无一符合。然而他们不愿意改团名，更不想剪头发。他们自己用报废的收音机拼装出堪用的音响器材，一面做工赚钱，一面想尽办法登台演出。一场典型的七○年代“宇宙塑料人”演唱会通常是这样展开：表演开始前两三天，耳语悄悄在熟人之间传开――据说“宇宙塑料人”要表演，地点可能是一个城郊谷仓，也可能是某人的结婚典礼，或者是一片林间空地。确切的时间地点，要到演出当天傍晚才公布。一旦地点确定，便会有几十个人，千里迢迢坐火车到一个不知名的小站，然后长途跋涉，穿越森林、踏过雪地、顶着风雨，来到一座波希米亚农庄。众人摸黑踏进约定的那个谷仓，运气好的话，“宇宙塑料人”会在里面，准备办一场秘密演唱会。 <br />
　　<br />
<br />
　　然而，消息灵通的秘密警察往往会循迹而至，打断演出，让所有心血通通泡汤。 <br />
<br />
　　这类故事中，最出名的莫过于一九七四年的“布多维茨‘大屠杀’”：这年三月，一千多个年轻人费尽千辛万苦跑到小镇布多维茨（Budjovice），准备看“宇宙塑料人”的表演，没想到警察早就等在当场。这些年轻人被押进黑漆麻乌的隧道里，惨遭警棍痛殴，然后整批送上火车，开回布拉格。几百名青年录了口供、六人被正式逮捕、几十个学生被退学――整个事件之中，“宇宙塑料人”根本还没开口唱歌呢。 <br />
<br />
　　回头说马哥。一九七六年，就在马哥跟哈维尔相识之后不久，他决定结婚，而且要大宴宾客。不用说，婚宴变成了一场“宇宙塑料人”和所有捷克地下乐团的马拉松演唱会。就在“地下社会”的亲朋好友共聚一堂、狂欢正酣的时候，秘密警察破门而入，所有参加演唱会的人都被抓去录口供，二十二个人被拘捕，大批录音带、歌曲手稿和乐队自制的控音器材都被没收。 <br />
<br />
　　“宇宙塑料人”的团员在一场样板审判中，被形容成吸毒酗酒性乱交打架滋事的地痞流氓，他们是“堕落的象征、社会的毒瘤”。国家机器动员大批人马抹黑这几个年轻人，让一般大众相信这只不过是单纯的刑事案件。最后，马哥和“宇宙塑料人”团员被判处八到十八个月不等的徒刑，“捷克青年永远不会在这种音乐之下起舞！”判决书上张牙舞爪地吼道。 <br />
<br />
　　哈维尔大为愤怒，决定展开救援“宇宙塑料人”的行动。他认为，假如大家都认同当局的说法，觉得这些年轻人罪有应得，那么这个社会可以算是完蛋了。“这些青年根本没有政治历史，甚至没有明确的政治立场，他们只不过是想按自己喜欢的方式过活，创作自己喜爱的音乐，唱自己想唱的歌，不与自己过不去，”他义愤填膺地说，“政权可以把所有独立思考、独立表达意见的人（即使只是私下思考和表达意见）都关起来……权力不知不觉暴露出它的真正意图：要让生活变得千篇一律，凡出现稍有不同的、个人的、突出的、独立的，甚至于不能归类的事物，都要用手术刀切除移走”。 <br />
<br />
　　没有人想象得到，这个救援事件竟成为捷克历史的转折点。尽量用简单的说法浓缩这段故事吧：哈维尔发起的联署抗议活动，竟发展成捷克知识分子在一九六八年之后的首次大串联。这群人觉得民意可用，决定一鼓作气，集体起草了“七七宪章宣言”，正式和当政者杠上了。哈维尔身为“七七宪章”的发言人，自此屡遭迫害，数度进出牢房，成为东欧最著名的政治犯之一。他在一九八九年最后一次出狱后不久组成“公民论坛”，在“丝绒革命”中促成了捷克当局的下台。 <br />
<br />
　　后来，就像全世界都知道的，哈维尔当选捷克共和国的总统，成为地球上最喜欢摇滚乐的国家领导人。 <br />
