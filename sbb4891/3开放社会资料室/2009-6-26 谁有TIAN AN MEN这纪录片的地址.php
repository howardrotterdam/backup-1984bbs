新纪元】坦克人与天安门<br />
 <br />
作者：徐沛<br />
&nbsp;&nbsp;<br />
<br />
【大纪元6月23日讯】“坦克人”是六四精神的象征<br />
<br />
一九八九年六月四日北京发生大屠杀后，一个勇于只身上前阻挡坦克车队进入天安门的身影传遍了世界，从此成为六四精神的象征。<br />
<br />
这个标志性的人物促使汤玛士（Antony Thomas）制作了一部有关六四运动的专题片《坦克人》（2004）。<br />
<br />
这位感动了世界的《坦克人》却被中共媒体诬`为“螳臂挡车的歹徒”。在我眼里，“坦克人”还代表了一大批敢于挑战强权的义勇之士。这批“六四暴徒”比六四学生遭到了更严酷的迫害，有的甚至立即被枪毙。<br />
<br />
在过去的二十年，中共势力制造了无数谎言来诬`六四运动和运动中产生的杰出人物，然而六四学生为民请命、勇于牺牲自己并感动了民众的事实胜于中共的宣传。六四运动中的英雄事迹不仅没有被时间埋没，相反，大浪淘沙后金子正在闪光。<br />
<br />
五年前，有个文坛剽客打着“天安门之子”的旗帜指责六四运动的风云人物，但他很快就在也属六四一代的郭飞雄的反衬下露出了真面目。这种鲁迅似的名利之徒令我鄙视，他谴责的六四明星却令我敬佩。<br />
<br />
六四亲历者，当年的美国留学生赛颂菲（Valerie Samson）今年在旧金山总图书馆首映她当年拍摄的录像时表示，“学生和百姓们是平和的，他们开始来天安门，只是为了吊唁冤死的胡耀邦。但是，他们面对的是不理睬、不平等、不讲理、不对话。”<br />
<br />
正是中共的蛮横无理促使学生们奋起捍卫人的尊严，多亏他们的无畏和奉献才促使中共在世界范围内暴露了其反人类的邪恶本质。是六四运动，尤其是六四屠杀唤醒了无数被中共欺骗的民众，我算其中之一，我也因此一直心存感激。<br />
<br />
在六四屠杀的震撼下，不少人明白了共党靠暴力、谎言（宣传）和共特来维持其专制统治。退党潮第一次在华人中涌现。<br />
<br />
在过去的二十年里，有关六四的作品层出不穷，在《坦克人》之前，我还看过卡玛夫妇拍摄的《天安门》（1995），遗憾的是我没有看到卡玛质疑的《移山》（1994）。不过《移山》的主角李录在成功逃离中共魔掌后，到过德国，受到留学生们的热烈欢迎，我也置身其中。李录能在人才济济的天安门广场上脱颖而出，用才华征服众人，并成为中共势力企图暗杀的对象足以令我敬佩。<br />
<br />
我庆幸李录们没有坚持用自己的鲜血来唤醒民众的初衷，多亏现在流亡英国的邵江当年在长安街目睹共军杀人后赶回广场力主撤退。几千“不自由，毋宁死”的天之骄子得以幸存并成为流亡世界各国的天安门一代。他们的存在就是对中共的抗议。<br />
<br />
《天安门》是六四精神的反面<br />
<br />
我看了《天安门》后，虽然十分纳闷，但觉得卡玛夫妇没有功劳，也有苦劳，毕竟他们关注六四运动。<br />
<br />
然而当我面对被称为“六四画家”的武文建对柴玲的无理指责时，我才意识到《天安门》的负面影响多么巨大。对此二十年来坚持整理六四史实的封从德认为：“卡玛这部电影从效果和剪辑的手法看，对柴玲是有恶意的和妖魔化的，比如它把柴玲引述别人的讲话全部剪掉，然后变成柴玲自己的讲话。‘天安门’这部电影就是这么处理的。整个电影对‘八九’年的学生运动来说，也是一个很大的歪曲，对‘八九’年历史的了解也是误导的。”<br />
<br />
二十年前武文建还是个爱好绘画的工人。他目睹共军滥杀无辜的罪行，“又听说王丹、柴玲、吾尔开希都被大兵打死”后义愤填膺，回厂讲述真相，号召罢工，很快被非法抓捕并被以“反革命宣传煽动罪”判处七年徒刑。<br />
<br />
武文建无法像我一样自由地接触与六四相关的人物和信息，所以，难以获知《天安门》既不客观，也不公正。否则怎么可能达到让武文建对柴玲的态度来了一个一百八十度的大转变？可惜武文建却误以为卡玛夫妇“作为外国人在当时能够记录八九年历史并客观地评论，非常值得称赞。”<br />
<br />
既然如此，我就有必要站出来说句公道话。而柴玲确实有理由起诉制作人，捍卫自己的清白，况且这涉及六四运动的声誉。《天安门》把柴玲的肺腑之言蓄意剪接，中间裁掉八千九百字，以达到让不知情的人得出她“让别人流血，而自己求生”的错误印象。<br />
<br />
卡玛在访谈中曾表示“谎言，无论出自于什么人，无论它的内容是什么，都只能损害说谎者的信誉。”所以，我不明白卡玛为何要明知故犯。<br />
<br />
除此之外，卡玛还表示，“理想主义不是共产党专有的，共产党恰恰是对很美好的理想主义的一种异化，一种歪曲。一旦中国共产党说的那些有关理想的大话破产之后，有些人一说起来关注别人、利他的思想似乎都是共产党的东西，一律斥之为左，似乎都没有存在必要了――实际上并不是这样的。美国这个社会，就是因为有很多有理想的人、有利他行为方式的人，才呈现出很多人性、活力的方面。我很珍视自己的理想主义的人生观，但我并不认为我自己的理想主义与共产党有什么相通之处。有人说：‘人之所以比飞禽走兽显得更有希望一些，不全在他们脑瓜特别机灵，而在于他们中间居然产生了扶穷济困的伦理思想，居然出现了与智力低的、相貌丑的、身体弱的、肢体残的姐妹弟兄同舟共济的道德行为。’我认为说得很好。”<br />
<br />
如果卡玛持“理想主义的人生观”，那她为什么不理解柴玲们的理想和奉献？认识不到六四运动的实质就是追求社会公义的理想主义运动？<br />
<br />
卡玛不是一般的外国人。她在北京长大，是个像大陆人一样从小被迫接受中共愚民教育的美国人。她也像我一样二十二岁才得以脱离“动物农场”，但从她的作品和访谈来看，她显然打上了共产党的烙印，而共产党那一套与理想主义无关，是用来骗人的谎言。<br />
<br />
原本是共党成员的丁子霖在爱子蒋捷连被枪杀后二十年也终于认识到，“是儿子的死，让我逐步走出了共产党文化给我造就的那份愚昧，让我恢复一个做人的尊严，做人的良知，每当想到这一点是用我儿子的鲜血和生命换来的，我真是既感到伤痛，又感到非常惭愧。”遗憾的是丁子霖在拍《天安门》时还没能认识这一点，否则，可能促使卡玛像我一样珍惜柴玲的肺腑之言。<br />
<br />
无论如何，我不愿相信卡玛是“铁杆毛分子”，虽然反毛泽东的先锋王容芬对卡玛的评论《红卫兵―毛分子―反人类―分析一个为文革张目的野蛮网站》<a href="http://www.observechina.net/info/artshow.asp?ID=46897" target="_blank">http://www.observechina.net/info/artshow.asp?ID=46897</a>有理有据。而这样也就可以解释卡玛对六四学生的态度了。<br />
<br />
批评《移山》，赞赏卡玛的戴晴虽然认识到“中国共产党，从根上就不正”，但她却一再攻击努力拔掉邪根的天安门一代，今年又提出什么“大和解”。<br />
<br />
我也难以相信卡玛与戴晴都在扮演对中共“小骂大帮忙”的角色。<br />
<br />
但愿这是对她们的误解，也希望她们扪心自问！<br />
<br />
西历二零零九年圣灵降临节 于德国科隆 ◇ <br />
