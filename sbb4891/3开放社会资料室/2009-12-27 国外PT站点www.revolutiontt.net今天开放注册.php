<br />
Private Tracker 是一类BT下载站点的统称。与Public Tracker相对。以下简称PT。<br />
<br />
大家所熟知的Public Tracker，比如minivova，eztv，thepiratebay等等，均是不需要注册成为网站用户，即可使用该种子下载的。<br />
<br />
PT是相反的，仅对于注册会员可用。会员才能够使用该站点的种子下载，有唯一的passkey来标志每一位会员，tracker server通过识别它来为会员累计上传量和下载量。<br />
<br />
成为PT会员，需要从PT会员手中得到邀请，或者在PT站点开放注册时间注册。<br />
<br />
二、PT的几个关键词<br />
<br />
Seeder：做种者<br />
Leecher：下载者<br />
UL：用户上传量<br />
DL：用户下载量<br />
Ratio：用户的UL / DL，一般指全局比例。<br />
Hnr or H'n r：Hit and Run。举例而言，当用户单种的ratio如果不达到0.8 or 1.0，站点会为该用户计算一个hnr点数，当hnr点数累计到某5次，封号。各个站点对hnr的要求各异，有不要求的，也有用延长做种时间来消除hnr次数的，也有实时hnr检测的。<br />
<br />
三、PT站点分类<br />
<br />
按内容分，有专做movie, tv, aname, music, sport, e-learning, 0day的站点, 另外还有一些综合性的站点，一些内容比较奇特的站点，比如魔术、恐怖等内容。<br />
<br />
<br />
<br />
四、PT的优势<br />
<br />
内容全面，新资源广泛，并且一些老资源也能找到，比Public Tracker的时效性长。<br />
质量精当，很多内容质量较差的种子，要不不予发布，要么会删除，留下来的都是优质内容。<br />
速度保障，对于现时的热种，速度很高；对于老种、冷种，也有大部分人保种，并且REQ系统一般都很高效。<br />
<br />
五、如何在PT中生存<br />
<br />
seeding， keep seeding是最好的办法。另外，从别处下载后到PT站上传也是有很有效的一个办法</div>

							
							
							
							
