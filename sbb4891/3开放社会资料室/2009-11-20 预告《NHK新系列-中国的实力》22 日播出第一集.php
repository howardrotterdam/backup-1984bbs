<br />
<br />
新系列和“中国 12亿人的改革开放“系列、“激流中国”系列一样，由角英夫担任总制作人，据说是在制作激流系列时就有了这个构想。他说希望观众能通过这个系列，改变对中国的陈旧观念，理解真实的中国形象。<br />
