2009年10月11日<br />
<br />
第9讲：《雇枪？》<br />
内战期间，男子被征召到前线作战-但新兵被允许可花钱雇人来顶替他们。Sandel教授问学生：这项政策是自由市场交易的例子么？或者这是某种形式的胁迫？因为较低的阶层去服役肯定出自更多的经济诱因？这引发了对战争和征兵等当代问题的课堂辩论。今天的志愿军真正是自愿的么？是否很多新兵都来自于不合比例的经济低下的背景？“爱国主义”起到了什么样的作用？什么是公民的义务？公民是否该有为自己国家服兵役的义务？<br />
<br />
<br />
第10讲：《出售母亲》<br />
Sandel教授把自由市场交易运用到当代颇具争议的新领域：生殖权利。Sandel描述了现代的“精子和卵子捐赠”交易中那些奇怪的父母。紧接着Sandel把辩论引向深入，他讲到了“Baby M”事件，此著名案例曾引发“孩子是谁的？” 的问题和矛盾。事情是这样：80年代中期，Mary Beth Whitehead和一对新泽西的夫妇签订了一项合约，同意为他们做“代孕母亲”，条件是支付给她一大笔的费用。但分娩之后24小时，whitehead决定留下这个孩子，于是双方不得不对簿公堂。学生们讨论了出售生命的道德问题，争论点围绕承诺，契约和母亲的权利。<br />
<br />
<br />
<br />
 <br />
<br />
考虑你的动机/道德的最高准则<br />
<br />
<a href="http://forum-network.org/lecture/mind-your-motive-supreme-principle-morality" target="_blank">http://forum-network.org/lecture ... -principle-morality</a><br />
<br />
2009年10月18日<br />
<br />
第11讲：《考虑你的动机》<br />
Sandel教授在课程中这样介绍康德：最具挑战性和最有难度的思想家之一。康德认为，我们作为个体，是神圣的，是权力的享有者，但并不是因为我们拥有自己。相反，理性和自由选择是我们的能力，使我们变得独特，使我们跟单纯的动物区别开。当我们将责任付诸行动的时候（去做正确的事），只有这样，我们的行动才有道德的价值。Sandel引用了一个例子：一名店主拒绝给一个顾客换零钱，只因他担心会影响他的生意。根据康德的理论，这不是一种道德行为，因为他没有找到正确的理由做正确的事情...。<br />
<br />
<br />
第12讲：《道德的最高准则》<br />
康德说，就我们的行为的道德价值而言，赋予它道德价值的是我们超越自身利益和偏好，将责任付诸行动的能力。Sandel讲述了一个真实的故事：一名13岁的男孩赢得一项拼字比赛的冠军，但随后他向法官承认，其实他把最后一个单词评错了。用这个故事和其他的故事，Sandel解释了用康德的理论如何来确定一项行动在道义上是否正确：在作出决定时，想象一下，你的行为背后的道德原则，是否能成为每个人都必须遵照的普遍法律。这个准则，是否能作为一个普遍规律，让所有人都受益？<br />
<br />
<br />
<br />
 <br />
<br />
谎言的教训/协议就是协议<br />
<br />
<a href="http://forum-network.org/lecture/lesson-lying-deal-deal" target="_blank">http://forum-network.org/lecture/lesson-lying-deal-deal</a><br />
<br />
2009年10月25日<br />
<br />
第13讲：《谎言的教训》<br />
康德的道德理论，严格到不允许有任何例外情况，他认为如果说谎，即使是善意的谎言，都是对自己尊严的侵犯。用一个假设的案例来考验他的理论：如果你的朋友藏在你家，一个杀手敲你的门来问他在哪里，你会对他说什么--不要说谎--来救你的朋友？这引发了对“误导性的真理”的讨论-以及对克林顿总统利用精确的语言来否认与莱温斯基的性关系，而没有直接向公众撒谎的讨论。<br />
<br />
<br />
第14讲：《协议就是协议》<br />
Sandel介绍了现代哲学家John Rawls和他的“假定契约”理论。Rawls认为，实现最公正和公平的治理的唯一途径是，如果所有立法者都平等的站到谈判桌前。试想，如果他们都在“无知的面纱”之后 C在他们的个人身份信息暂时不公开（他们的种族，阶级，个人兴趣）的时候，他们必须就一系列法律达成共识。Rawls认为，只有这样，治理机构才能商定真正公平公正的原则。<br />
<br />
 <br />
<br />
<br />
什么是公平的起点？/我们该得到什么？<br />
<br />
<a href="http://forum-network.org/lecture/what%E2%80%99s-fair-start-what-do-we-deserve" target="_blank">http://forum-network.org/lecture ... -what-do-we-deserve</a><br />
<br />
2009年11月1日<br />
<br />
第15讲：《什么是公平的起点？》<br />
John Rawls把他的“无知的面纱”理论运用到社会和经济平等的争论焦点中，以及公平治理的问题。他问，如果每个市民都必须参与税收的再分配问题―在他们不知道最终会成为社会成员中的穷人还是富人的时候-我们大多数人不是更喜欢消除金融风险并同意财富的公平分配么？<br />
<br />
<br />
第16讲：《我们该得到什么？》<br />
Sandel教授重述了三种不同的理论，涉及如何在生活中分配收入，财富和机会。他总结了自由主义，精英制度系统和平等主义理论，引起了对当今社会薪酬差别的公正性的讨论。Sandel比较了美国联邦最高法院O’connor大法官（200 000美元）和法官Judy（2,500万美元）的工资。Sandel问大家，这是否公平？如果不是，原因何在？Sandel解释了John Rawls的观点-他认为，个人的“成功”往往是和荣誉无关的随机结果：运气，继承的财富，积极的家庭环境。但是，对于付出更多努力和更长时间去获得成功的个体-如何来衡量他/她付出的努力呢？<br />
<br />
<br />
 <br />
<br />
讨论反歧视行动/目的是什么？<br />
<br />
<a href="http://forum-network.org/lecture/arguing-affirmative-action-what%E2%80%99s-purpose" target="_blank">http://forum-network.org/lecture ... t%E2%80%99s-purpose</a><br />
<br />
2009年11月8日<br />
<br />
第17讲：《讨论反歧视行动》<br />
学生们讨论反歧视行动和大学招生问题。学校在招生的时候考虑种族和族裔因素是否正确？是否侵犯了个人权利？是否和喜欢一个明星运动员一样平等和主观？这样的争论是不是倾向于把推行多样化变成合理的呢？这个观点应该如何来反对一个学生的努力和成绩更重要的观点呢？<br />
<br />
<br />
第18讲：《目的是什么？》<br />
Sandel介绍亚里士多德关于公平和正义的理论，简单地说，是告诉人们他们该付出什么，该得到什么。亚里士多德认为，一个人在考虑分配问题的时候，必须考虑分配的目标，终点和目的。对他来说，这是关于一个人找到合适的位置来发挥他的美德的事情。<br />
<br />
<br />
<br />
 <br />
<br />
好市民/自由与适应<br />
<br />
<a href="http://forum-network.org/lecture/good-citizen-freedom-vs-fit" target="_blank">http://forum-network.org/lecture/good-citizen-freedom-vs-fit</a><br />
<br />
2009年11月15日<br />
<br />
第19讲：《好公民》<br />
亚里士多德的正义理论引起了有关高尔夫球的辩论，特别是高尔夫球的“目的”。学生们辩论美国PGA在这件事上的做法是否错误--不允许残疾人选手Casey Martin在职业巡回赛中使用球具手推车。<br />
<br />
<br />
第20讲：《自由与适应》<br />
Sandel指出了对亚里士多德关于自由的观点最突出的反对声音―他为奴隶制的辩护。学生讨论对亚里士多德理论的其他反对意见，并辩论他的哲学是否限制了个人自由。<br />
<br />
 <br />
<br />
<br />
社会的需求/我们的忠诚在哪里<br />
<br />
<a href="http://forum-network.org/lecture/claims-community-where-our-loyalty-lies" target="_blank">http://forum-network.org/lecture ... re-our-loyalty-lies</a><br />
<br />
2009年11月22日<br />
<br />
第21讲：《社会的需求》<br />
Sandel教授介绍康德和John Rawl对亚里士多德理论的反对意见，亚里斯多德认为个体应该有自由有能力选择他的终极目标。这引来了关于共产主义观点的介绍。作为个体，我们该如何衡量我们对家庭的义务，对社会的义务和对国家的义务呢？<br />
<br />
<br />
第22讲：《我们的忠诚在哪里》<br />
Sandel教授发起了一个讨论：在各种社会群体中，小到家庭这样的群体，大到社会，我们是否有团结协作的义务和成员的义务。针对不同的事件，学生们辩论忠诚是否比责任更重要，何时更重要。<br />
<br />
<br />
<br />
 <br />
<br />
辩论同性婚姻/美好生活<br />
<br />
<a href="http://forum-network.org/lecture/debating-same-sex-marriage-good-life" target="_blank">http://forum-network.org/lecture ... -marriage-good-life</a><br />
<br />
2009年11月29日<br />
<br />
第22讲：《辩论同性婚姻》<br />
如果公正的原则取决于权利服务的终点是否有道德或内在的价值。社会怎样处理不同人对“好”持有不同的想法和观念？利用同性婚姻的例子，学生辩论是否可能把性的道德合法性从婚姻的最终目的中脱离出来。<br />
<br />
<br />
第23讲：《美好生活》<br />
Sandel教授提出了两个问题。是否有必要找到美好生活的原因，以决定什么是正义，什么是人民的权利？如果是那样的话，是否可以争论或者证明美好生活的本质是什么？学生辩论时，进一步讨论了政府在决定婚姻目的这个问题上的作用。Sandel作总结时指出，我们作为个体，可能永远不会同意许多道德哲学问题的争论点。不过他认为，一方面，关于这些问题的辩论是不可避免的。另一方面，这给我们提供了一个更好地了解他人价值观的好机会。</div>

							
							
							
							
