													<h2>最受欢迎的哈佛新生公共课--关于公平与正义[系列视频]</h2>
						
						
																											<div id="postmessage_589501" class="t_msgfont">关于公平和正义的系列讲座<br />
<br />
<br />
<br />
 <br />
<br />
该讲座以哈佛教授Michael Sandel的 《关于公平和正义的入门课》为基础，是对道德和政治哲学的系列入门介绍。<br />
<br />
这套讲座共有12集，邀请观众们带着批判的观点来思考正义，公平，民主和公民权等基础问题。在哈佛大学，每星期都有一千多名学生去听教授兼作家的Michael Sandel开设的这门课程，渴望藉此扩充对政治和道德哲学的理解，并从中检验长期秉持的信仰。学生们学到了过去的伟大哲学家们的哲学理论-亚里士多德，康德，穆勒，洛克--再把学到的东西运用来思考复杂且动荡不定的现代社会的种种问题，包括反歧视行动，同性婚姻，爱国主义，忠诚和人权。<br />
<br />
<br />
<br />
 <br />
<br />
演讲者：Michael Sandel (哈佛大学哲学教授)<br />
<br />
（点击以下超链接地址可观看相关视频）<br />
<br />
 <br />
<br />
谋杀的道德侧面/同类相残案<br />
<br />
<a href="http://forum-network.org/lecture/moral-side-murder-case-cannibalism" target="_blank">http://forum-network.org/lecture ... er-case-cannibalism</a><br />
<br />
2009年9月13日<br />
<br />
第1讲：《杀人的道德侧面》<br />
如果必须选择杀死1人或者杀死5人，你会怎么选？正确的做法是什么？教授Michael Sandel在他的讲座里提出这个假设的情景，有多数的学生投票来赞成杀死1人，来保全其余五个人的性命。但是Sandel提出了三宗类似的道德难题-每一个都设计巧妙，以至于抉择的难度增加。当学生站起来为自己的艰难抉择辩护时，Sandel提出了他的观点。我们的道德推理背后的假设往往是矛盾的，而什么是正确什么是错的问题，并不总是黑白分明的。<br />
第2讲：《同类自残案》<br />
Sandel介绍了功利主义哲学家Jeremy Bentham（杰瑞米・边沁）与19世纪的一个著名案例，此案涉及到的人是4个失事轮船的船员。他们在海上迷失了19天之后，船长决定杀死机舱男孩，他是4个人中最弱小的，这样他们就可以靠他的血液和躯体维持生命。案件引发了学生们对提倡幸福最大化的功利论的辩论，功利论的口号是“绝大多数人的最大利益”。<br />
<br />
<br />
<br />
 <br />
<br />
给生命一个价格标签/如何衡量快乐<br />
<br />
<a href="http://forum-network.org/lecture/putting-price-tag-life-how-measure-pleasure" target="_blank">http://forum-network.org/lecture ... ow-measure-pleasure</a><br />
<br />
2009年9月20日<br />
<br />
第3讲：《给生命一个价格标签》<br />
Jeremy Bentham（杰瑞米・边沁）在18世纪后期提出的的功利主义理论-最大幸福理论 -今天常被称为“成本效益分析”。Sandel举出企业运用这一理论的实例：通过评估衡量一美元在生活中的价值来作出重要的商业决定。由此引起了功利主义的反对观点的讨论：即使当多数人的利益可能是卑鄙或不人道的时候也强调绝大多数人的利益，这样是否公平？<br />
<br />
<br />
第4讲：《如何衡量快乐》<br />
<br />
Sandel介绍另一位功利主义哲学家J.S. Mill（穆勒，也译作“密尔”）。他认为，所有人类的体验都可以量化，但某些快乐是更值得拥有，更有价值的。穆勒认为，如果社会重视更大程度的欢乐和公正，那么长远来说，社会整体终会有所进步。Sandel的检验这个理论的方法是， 在课堂上播放了3个视频剪辑《辛普森 》 ，真人秀《勇敢者的游戏》，以及莎士比亚的《哈姆雷特》 -然后要求学生辩论：这三个体验里的哪一个应该被定为“最大程度”的快乐。<br />
<br />
<br />
<br />
 <br />
<br />
自由选择/我属于谁？<br />
<br />
<a href="http://forum-network.org/lecture/free-choose-who-owns-me" target="_blank">http://forum-network.org/lecture/free-choose-who-owns-me</a><br />
<br />
2009年9月27日<br />
<br />
第5讲：《自由选择》<br />
自由主义者认为政府干预最少的社会是最理想的社会形态。Sandel介绍自由主义哲学家Robert Nozick，他认为，每个个体都有选择自己想要的生活的基本权利。政府不应该具备权力来制定人们保护自我的法律（安全带法），不应该具备权力来制定把道德价值观强加给社会的法律，不应该具备权力来制定把富人收入重新分配给穷人的法律。Sandel使用了比尔盖茨和迈克尔乔丹的例子来解释Nozick的理论：税收的重新分配是强迫劳动的一种形式。<br />
<br />
<br />
第6讲：《我属于谁？》<br />
自由主义哲学家Robert Nozick举出了一个向富人征税的例子―房屋税，医疗保健，穷人的教育-这是一种强制。学生们首先讨论了支持重新分配税收的观点。如果你在一个具有税收制度较为进步的社会系统中，你是不是被强制交税？穷人到底需不需要那些他们已经获得的社会服务吗？他们值得享受那些服务么?富人们是不是经常通过侥幸或家庭财产获得财富？在这个讲座里，一组学生（名为“自由意志队”）被要求为自由意志的反对意见辩护。<br />
<br />
<br />
 <br />
<br />
这片土地是我的/满合法年龄的成年人<br />
<br />
<a href="http://forum-network.org/lecture/land-my-land-consenting-adults" target="_blank">http://forum-network.org/lecture/land-my-land-consenting-adults</a> <br />
<br />
2009年10月4日<br />
<br />
第7讲：《这片土地是我的土地》<br />
洛克（John Locke）既是自由意志论的支持者，也是它的批评者。Locke指出，在“自然状态”，在任何政治体制建立之前，每个人都享有生命，自由和财产的自然权利。然而，一旦我们同意进入社会，就同意了受法律制度的约束。因此，Locke认为，即使政府干预了个人的权力，这也是大多数人的意见赋予了它权力这么做的。<br />
<br />
<br />
第8讲：《满合法年龄的成年人》<br />
洛克谈到税收和同意的问题，他是如何面对以下两个问题的困扰：1）他认为个人的生命，自由，财产具有不可剥夺的权利 2）政府通过大多数的条例- -未经个体同意，就可以向他们征税？难道这不等于未经他/她的同意，掠取他的个人财产？洛克的答案是，我们正在通过社会生活对税收法律做“默认同意”，因此，税收是合法的。而且，只要政府不是特意对某一群体征税-如果不是武断专横的-那么税收并没有侵犯个人的基本权利。<br />
<br />
