<br />
　　中国每年的伐区剩余物超过3,600万立方米，而实际利用了的仅1、20万立方米，千分之三以下。<br />
<br />
　　全国范围内，每年当柴烧掉的木材，占森林资源消耗总量的1/3。<br />
<br />
　　中国的原木利用率低於50%，加工剩余物的利用率则更低。六十年代，中国木材加工剩余物的利用率与罗马尼亚不相上下。现在，罗马尼亚上升到60%以上了，中国还只有10%左右。<br />
<br />
　　★牡丹江林区东京城林业局近年来至少非法砍伐了3万立方米林木，并高价销售，牟取了2千万元以上的暴利。<br />
<br />
　　大规模的群众性的哄抢盗伐浪潮，1985年曾席卷南方九省（区），一年之内，森林资源损失1.6亿立方米，使能够提供商品材的县由近300个减少到172个。<br />
<br />
　　88年根据辽宁、浙江、湖南等七省统计，森林案件比上年增加20%以上，经济损失也比上年上升30%以上。全国上半年发生毁林案件2万多宗……在某些地区，盗伐和抢伐已形成了一门新的行业，如武装砍伐队、盗林运输队、为盗伐抢伐木材提供买卖资讯的掮客，甚至还出现了专门为“会员”支付被抓和罚款损失的盗林“基金会”。<br />
<br />
　　林业部门的官员则与违法者沆瀣一气，甚而高价出售木材出境许可证等文件，分享暴利。<br />
<br />
　　★1993年，中国林业部长徐有芳指出：经数十年乱砍滥伐，中国大陆的成材几乎完全砍伐殆尽。<br />
<br />
　　森林蓄积量，就是衡量森林资源的收支平衡情况。……中国森林蓄积量一直呈急剧减少的趋势，砍伐量远远超过生长量，赤字极大。<br />
<br />
　　中国人工林成活率低、质量差，每公顷蓄积量仅28.27立方米，是德国人工林360立方米的7.8%，……<br />
<br />
　　中国人工林树种单一、生态失调，病虫害多发并日趋严重，被称为“不冒烟的森林火灾”。<br />
