第二章・水土流失的怪圈<br />
<br />
<br />
　　★陕西省延川县某行政村为保住100多亩坝地……修筑起一道大坝。但1990年一场并不愤怒的山洪弹指一挥，大坝就被粉碎了。……村民咬咬牙集资5,000元立即补上大坝。但仅隔十余天，一场大雨，山洪再度暴发，大坝又纸墙一样破了……村民……再集资5,000元，据说把娶媳妇、做寿材的钱也用上了。三天以後，又一场大雨，大石坝再次……老老少少坐在泥水中仰天大恸！<br />
<br />
　　★黄土高原水土流失剧烈的7.7万平方公里，年土壤侵蚀量可达每平方公里万吨以上；其最惨烈之处，竟达2万吨以上。<br />
<br />
　　长江每年土壤侵蚀量为22～25亿吨，相当於每年失去39厘米厚的耕地830万亩。<br />
<br />
　　与此同时（1957～1986），长江流域森林覆盖率从20.2%减少到10%；水土流失面积从36.38万平方公里、占流域总面积20.2%，增加到73.94万平方公里和41%。<br />
<br />
　　长江每年土壤侵蚀量为44～50亿吨。按1/6入海计算，则长江每年入海的泥沙量应有7、8亿吨。已远远超过黄河泥沙入海量年均4亿吨之数，并相当於尼罗河（1.11亿吨/年）、亚马逊河（3.62亿吨/年）、密西西比河（3.12亿吨/年）输沙量之总和（7.84亿吨/年）。而且，长江的径流量与流域面积仅为三大河的1/5和1/7。<br />
<br />
　　长江中上游山地丘陵区的1.74亿人口，在十五至四十年後将失去耕地4,000万亩，人均耕地将从1.17亩降到0.5亩，必然最终失去生存之地。<br />
<br />
　　东北黑土平原……历史上几十厘米厚的黑土层已流失过半，仅剩下20厘米左右，已达到或接近没有耕作价值的母质。有些地方甚至流失殆尽。<br />
<br />
　　中国大陆水土流失的面积为367万平方公里，占全部国土面积的将近40%。<br />
<br />
