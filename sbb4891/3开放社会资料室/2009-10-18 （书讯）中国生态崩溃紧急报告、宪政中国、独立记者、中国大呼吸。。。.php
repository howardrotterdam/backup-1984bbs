													<h2>导读：谁最有希望为中国未来设计政治制度</h2>
						
						
																											<div id="postmessage_288455" class="t_msgfont">黎安友(美国哥伦比亚大学教授)<br />
<br />
在过去的二十年里，中国经济改革成效巨大，但是相应的政治变迁则推迟了。经济改革的成功CC例如居民收入的提高，消费社会的扩展，流动的自由的增加以及人们可以自由选择自己的，也导致了社会紧张状态的出现，这种紧张状态或迟或早要通过更加开放的政治制度加以解决。不管人们想法如何，中国的当权者迄今为止偏爱于以威权主义手法来应对上述社会压力。但是，他们也已经或多或少地公开承认：在不久的将来，政治改革将要提上议事日程。<br />
<br />
没有人指望通过破坏性的手段带来最终的政治开放，这可以用来解释为什么多年来体制内外一些聪明的思想者和一些外国学者想方设法在思考在中国实行渐进的和平政治改革的原因。在这些众多思想者中间，宪政主义者以崭新的面目出现，他们最有希望为中国的未来设计出可行的、稳定的和公开化的政治制度。这种制度可以在中国进入全面现代化阶段时CC这是一个相对富裕和更加复杂的社会，具有更多的统治合法性。<br />
<br />
宪政主义与宪法是不可同日而语的。中国有宪法，但是中国不是立宪政府。宪政主义的概念包含法律至高无上，甚至法律高于统治者的意愿。宪政主义的要素不仅需要有通过白纸黑字规定下来，而且需要实实在在地在社会政治生活中体现出来。因此，宪政主义不仅需要建立恰当的制度性安排，这些制度要高于当权者本身，而且这些制度性安排能够有效实践法治。通过一九五八年的大跃进、长达十年的文化大革命、镇压一九七九年西单民主墙运动和一九八九年天安门运动，通过一个又一个的所谓反对枳什阶级自由化榈恼治运动，我相信中国人对宪政主义和宪法的这种重要区别已经一目了然。<br />
<br />
一部纯粹的栊丛谥缴系南芊é槭遣荒苡行У毓娣逗拖拗普治冲突的，在这种情形下，即使有所谓的柘芊é椋手握令牌的人仍然恣意妄为，侵害他人权益。如果没有当权者恪守的游戏规则，政治家们时常寻求新的方式发挥威力，其后果或者是导致腐败，或者导致暴力。<br />
<br />
拟就一部在实践中平安无事的好宪法并非一件轻而易举的事情，而发现什么应当写进一部具有权威性的宪法则更非易事。这即是说：在宪法包含着限制每个组织和个人权力的规定的时候，人们很不容易发现是什么样的社会的、思想的、文化的和政治的因素会导致政治家、军官、富有的企业精英们、强大的公众媒体以及一般大众发自内心地敬重和遵守宪法。<br />
上述问题的部分答案存在于这样一种精巧的宪法设计之中：以一个组织的权力限制另外一个组织的权力。<br />
<br />
然而制度是不能自我实施的，制度的实施需要强力和社会承诺，只有这样，才会无人胆敢逾越雷池，即使有人尝试这样，也会被其它多数人拒绝。<br />
<br />
培养宪政主义这样一种合法性，关键是要持之以恒。严肃理智的辩论会有助于整个社会和政治精英们对一般意义上的宪政主义的共识和理解，也有助于增加对特定的一组既定的宪法上的制度安排对社会的可行性和促进国家利益的理解。例如，在美国历史上，媪邦党人文集绮生了关于宪法安排的共识，在这本阐述美国立宪者思想的著作中，尽管作者们分别处在不同的政治境况之下，但是他们都从事了关于美国宪法的制度性安排的至关重要的辩论。<br />
<br />
宪政主义与民主有交叉重迭的关系。如果宪政主义意味依据程序规则限制政治家的行为的话，那么，在原则上可以存在一部非民主的宪法，中国和西方的历史上都存在过这种宪法。但是，在当代中国，我在这当然是指在后毛泽东和后邓小平时代的中国，中国人民教育程度不断提高、人民可以自由迁徙、经济繁荣、政治意识越来越具有现代性，宪政制度如果不承认人民主权是不可想象的。一部未来中国可行的宪法必须保障民主权利。<br />
<br />
要求在宪法上承认民主权利，这与这些民主权利是从资产阶级的西方历史中进口的枞巳é楦拍罨蛘呤侵谢人民共和国建立以后逐渐从马克思主义和苏联传统发展而来的韫民权利楦拍钔耆没有关系。承认民主权利的主要原因是：在中国，一个有效的宪政主义政权，CC即一个稳定的适应于迅速的经济和社会变迁的政治制度，将要确定公民如何表达其要求和意见以及从事政治参与的规则。除非中国的政治制度给予中国公民这种机会，这种机会不仅要写在宪法纸上，而且还要体现在实际中，否则，中国的政治制度难以实现安定团结。<br />
<br />
同时，这些规则必须设定公民参与的限度CC即不被许可的言论、结社和参与。这些限制在不同的社会可以不同的方式来规定。但是，有一些共同的核心内容是所有的宪政主义制度应当承认的，因为他们的存在是宪政制度有效运作所必须的。例如，所有以和平方式表达的政治问题的观点必须加以保障或者是宪政主义的基本精神是不容置疑的。但是，在不同的社会会确定不同的关于政治问题的界限，为了建立共识，必须进行持久和仔细的讨论，在宪政制度开始运作时，共识也会跟着宪政制度变化，变化和共识是相辅相成的。<br />
<br />
将来中国立宪政府要面对的一个重要问题是：现行宪法是否或者在什么方式上可以作为未来中国宪政制度的基础。在某些方面，现行的中华人民共和国宪法是未来中国宪政制度的较好基础，例如它承认公民的政治权利，规定了人民主权，确定了某种程度的司法独立，要求实行某些官员和某些层面的政府官员的选举。<br />
<br />
嫦苷中国缁卮鹆松鲜鑫侍庵械囊恍┪侍狻＞萁樯埽该项研究开始于一九九五年，完成于一九九七年初，由中国大陆数十位学者共同完成。参与此书写作的人来源于中国大陆各地的各个学术研究机构和大学，也包括一些在美国学习的中国学者。在该项研究的整个过程中，中国的政治局势仍然十分严峻，公开化的宪政改革讨论是不允许的，并要冒很大风险，因此，所有写作基本上都是基于本书主编与作者面对面商量的一个写作大纲，然后由各章执稿人独立完成。<br />
<br />
鉴于中国政府当局对该项研究的打压，该书的作者未使用自己的真实名字。这是可以理解的。值得指出的是，这些作者们以顽强的勇气和智慧完成了这项跨中国大陆各地区的十分不容易的合作研究。为了推动对未来中国意义重大的宪政化的讨论，他们提出了有关宪政主义的广泛理论问题和特定的制度化改革建议。<br />
<br />
这项研究以下述两种方式告诉人们将来宪政改革的方式：那些关心中国未来命运的人如何思考他们国家将来的事情，同时，它表明所有的中国人在面对未来的挑战时，在以比较实际的方式考虑建设自己国家未来的政治制度。</div>

							
							
							
							
