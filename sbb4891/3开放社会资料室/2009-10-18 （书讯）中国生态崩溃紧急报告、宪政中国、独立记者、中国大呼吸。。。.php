近年台湾历经政党对峙、政府空转、贪腐等社会乱象，当我们深受其苦，努力超越这些问题之际，其实早在其它发展民主的国家中，便可找到借镜。长久以来，一直致力于研究各国民主进程的知名社会学家赖瑞&#8231;戴蒙，藉由各国发展民主的故事，指出「民主」绝非一人一票选总统那么简单，更不仅仅是体制的改变而已，重要的是改变人心。他明确定义何谓「民主」，进而阐述哪些因素可促进或阻碍民主发展，描绘使民主得以运作的政府机制与公民社会，希望藉由他山之石，找出改善民主制度的关键，如此一来，当大部分的人想要改变领导人和政策时，便能在既有的规范中，运用有效率的组织来达到改变的目的。<br />
赖瑞&#8231;戴蒙指出民主的敌人不是回教的圣战运动、威权体制，而是人性的骄傲自大、不妥协、愚蠢与贪婪，在历史上随处可见因这些人性特点而垮台的民主故事，但是，他深信，人类具备从失败中学习的能力。未来，即便如中国如此强大的共产威权国家，也会因为经济发展影响社会结构与人民素质，而在下一世代转型为民主国家。<br />
民主，是与我们息息相关的生活形态，但它无法凭空获得，甚至只要我们对政治、对社会漠不关心，民主就会再度消失。我们的生活幸福与否，一切关键便在于，我们是否参与其中。阅读本书，当社会再度纷乱，你将具备清晰的思路逻辑、完整的背景知识，了解如何拨开迷雾，为建立美好社会尽一己之力。<br />
