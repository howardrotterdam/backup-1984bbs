　　整理戴旭：《2030肢解中国-美国全球战略与中国危机》报告中的经典部分 　　 <br />
　　戴旭，<a href="http://www.hudong.com/wiki/%E6%88%B4%E6%97%AD" target="_blank">http://www.hudong.com/wiki/%E6%88%B4%E6%97%AD</a> 　　 <br />
　　空军上校，生于河南民权县，山西运城市入伍。历任地空导航台长、新闻干事、秘书等职务。[1] 　　 <br />
　　曾出版《以血祭天》、《20世纪世界空战》、《读史随想》等著作，并同时为《环球时报》及人民网等媒体的专栏作家。[1] 　　 <br />
　　毕业于空军电讯工程学院和空军政治学院。发表过军事学术专著、政论和军事评论文章一百余篇，以见解独到、思想敏锐、观点尖锐、文风犀利、语言生动见长。[1] 　　 <br />
　　戴旭上校曾在媒体上公开发表《第27中队入侵 美国隐形空军与当代战争样式革命》，他在文中详细介绍了采用隐形技术的F-22A战斗机给美国空军所带来的前所未有的战术优势。与一些无知的网络写手相比，戴旭上校是敢于直面美军技术优势的第一人，让国人摒弃了“鸵鸟心态”。[2] 　　 <br />
　　以下是摘录了戴旭在《2030肢解中国-美国全球战略与中国危机》演讲视频报告中的部分精彩句子。本人感觉是难得的头脑还算清醒的人说的实话，很佩服在当今这个时期还有军人能说出如此真诚的实话。实属难得。 　　 <br />
　　经典语句1：今天中国的经济，有人说是房地产经济，我看有几分道理。在激素的刺激下，中国长成了一个没有骨头的大胖子。我们错把重量当力量，把肥大当强大了。 　　 <br />
　　经典语句2：中国人的钱是怎么挣来的？每年的矿难，环境污染，辛苦的打工者，中国的钱都是带血的。 　　 <br />
　　经典语句3：我认为美国欠中国的钱，是永远不会再归还了，至少不会等值归还了。就像一块肉进了狗肚子，你怎么还能指望它给你吐出来呢？我们有些人还要求美国保证我们美元资产的安全，这就等于跟那条狗说，你要保证我们那块肉的安全。狗一定会说：放心吧，你的肉在我的肚子里很安全！ 　　 <br />
　　经典语句4：它只是不断地借新钱还旧债，都是你的钱在循环。所以，表面上看起来它在不断的还，实际上是在吃你的肉，却在拉狗屎给你，因为它在通货膨胀，钱在贬值。它不可能真正还肉给你的，它怎么可能吐出来？ 　　 <br />
　　经典语句5：美国经济的运转，就是靠不停地借钱。一个美国小孩问巴菲特爷爷，您这辈子借的钱，将来是不是要让我们去还？巴菲特说：孩子，好好学习！让你的孙子替你还！那我们就看看，未来美国孙子是怎么还这笔天文数字的钱吧！ 　　 <br />
　　经典语句6：门蒂斯先生在演讲中，也回答了我之前对这个问题的质疑：他说，当初我们欠英国人的钱，我们把它打跑了。我们也欠过荷兰人的钱，我们也把它打跑了。我们现在也欠中国人的钱。本质上这没有什么不同，只是8000 万和8000 亿的区别。 　 <br />
　　经典语句7：马国书先生跟我说，这是他在开玩笑。可是我就是笑不出来。一个黑老大，”借“了你的钱，他带着打手、枪炮和狼狗，你一个干巴瘦的平民小老头敢跟他要？能要得回来？所以，现在它们关于保护我们资产的承诺，在我听起来就是放狗屁。这就是我们能指望的了，要么是狗屁，要么是狗屎。你要再不服，它还可以露出带血的狗牙给你看看：你是要钱，还是要命？！　　　 <br />
　　经典语句8：美国对付中国的战略，是一贯的，清晰的，不分党派。政治的、文化的、外交的、经济的、军事的，时而国际时而国内，时而搞个什么G2”中美国“概念，忽悠中国，离间中俄，时而又在两岸和中日之间左右逢源，在中国周边又打又拉，分化瓦解，笑里藏刀，组合拳，连环腿，步步紧逼，近20 年来可以说始终不给中国片刻喘息机会。而中国则是四面招架，防不胜防。总结一些美国对中国的做法，几乎可以编一本《折腾中国的千百个理由和做法》。奥巴马上台以后，很多人以为他可能比前任对中国好点，结果如何？连续两个反倾销，贸易保护主义先拿中国开刀。　　 <br />
　　经典语句9：全世界只有中国宣布用4 万亿带动20 万亿人民币造“铁路、公路”和基础设施。其他大国都在造军备。日本今年下水两条直升机航母，印度宣布将造3 艘航母，十艘核潜艇，美国已经制造了近200 架的F－22，还在继续大规模研制新一代武器，2009 年6 月成立网络战司令部。俄罗斯也在进行大规模的装备生产。 　　 <br />
　　经典语句10：中国一点都不想打仗，不想和美国搞对抗，但美国却费尽心机，折腾中国。就像牛一点都不想和老虎对抗，但老虎总是围着牛又吼又抓，还时不时咬一口。小布什上台，对中国咄咄逼人；奥巴马上台，又对中国展开不动声色的合围。以眼前论，美国深陷金融危机，不好好做自己的事，救自己的金融机构和实体经济，希拉里和奥巴马还到处穿梭，忙于包围中国，还制造14 吨的超级大炸弹。很多人想不明白为什么。 　　 <br />
　　经典语句11：历史并没有远去。1840 年，欧洲第一次对中国瓜分；1894 年，中国洋务运动也就是第一次改革开放失败，世界对中国进行第二次瓜分。在这50 年的两次瓜分中，中国失去了中华文化圈中的日本，朝鲜半岛；东南亚；台湾；东北大片领土以及蒙古。 　　 <br />
　　经典语句12：我一直有一种强烈的预感：未来的10 到20 年，也就是2020 到2030 年左右，会有一场针对中国的大屠杀，大哄抢。这不是危言耸听，而是有着历史的逻辑。 　　 <br />
　　经典语句13：到19 世纪它们发现了世界上最富裕的中国。1840 年的时候，中国富甲天下，到1940 年的时候，一百年中被世界抢得只剩下一片焦土，成为世界最穷的国家。发生在中国这片土地上的，称得上战争规模的就近百场。这些战争，就是在夺中国的财富。他们在中国的土地上打败中国，让中国赔款，割地。这跟一个强盗到人家里的行为有什么两样？有钱拿钱，没有钱拿东西，拿地，什么都没有就让人家借钱给他－－中国最后不是借欧洲的钱还日本吗？最后还杀人家的男人，强暴人家的女人，烧人家的房子，圆明园遗址不就是这样留下的吗？ 　　 <br />
　　经典语句14：把中国夺完以后，自己又互相夺。两次世界大战打累了，他们要进行第二次工业革命，发现了中东的石油，于是，又到中东去夺。中东又打了五十多年，一直到现在。 　　 <br />
　　经典语句15：现在，今天的中国，就和鸦片战争和洋务运动之后的中国一样，你有钱了，就是长肥了，所以食肉动物都来了。 　　 <br />
　　经典语句16：中国人一直梦想的是复兴和崛起，但在美国和日本的眼里，它们思考的是一只肥牛已经长大，何时开刀宰杀。一百多年前的洋务运动不就是如此吗？经过三十多年”改革开放“，中国积累了很多财富，日本突然扑上来，像一只野狼咬住牛蛋一样，把中国放倒，然后其他猛兽一起扑上，把中国洗劫一空。现在，中国改革开放又三十年了，又弄了不少钱，它们又眼红了。特别是看到很多中国人没有警惕不思进取，更觉得有机可乘。 　　 <br />
　　经典语句17：我预感未来中国要么会遇到巨大的内部或周边此起彼伏的战争，要么两者一起来。一旦打起来，乱起来，时间就短不了。我判断美国不会直接和中国打，因为大国之间动起来，地动山摇，而且美国从来都是让别人打头阵，等人家打累了，自己再上。可能先发生周边地区的战争，美国干预，先消耗中国。这些战争除南海以外都有可能引发核战争。等中国打累了周边也打累了，美国就该出来了，像两次世界大战一样，当渔翁，拣走亚洲五十年发展的成果。 　　 <br />
　　经典语句18：为什么是2020 和2030 年？看看美国给自己军队定的转型时间表就知道了。到2025 年左右，美国现在的四大军种，全部转型完毕。那时，它还有两个新军种，一个是天军，一个是网军，这两个是主力。六大军种，采取”一小时打遍全球“理论。美国以往对小国，是斩首行动；未来对大国，是快速战略瘫痪。 　　 <br />
　　经典语句19：美国的新军事战略。它”一小时打遍全球“的三大武器系统，一是安装常规弹头的三位一体的洲际导弹，二是空天轰炸机，三是太空武器和网络武器。之后，才是隐身空军和无人攻击机、机器人部队等。它的防御体系就是全球反导系统。攻防兼备。 　　 <br />
　　经典语句20：它们还通过美国公民全球的合法旅游，拍摄视频、照片，充实这一信息库。前两年中国在新疆和甘肃等地连续发现日本人搞测绘，其实就是在为日美提供战略信息。清朝时候日本就开始测量中国的地理了。现在又开始了。 　　 <br />
　　经典语句21：我们用票子吹起了一个又一个气球一个有一个泡沫。 　　 <br />
　　经典语句22：这一场战争，如果被我不幸言中，结局比一百年前还严重，我认为将被瓜分。 　　 <br />
　　经典语句23：2030 年，当我的泡泡吹到最大的时候，也正好是美国和其他列强新型军队打造完毕，战刀出鞘的时候。 　　 <br />
　　经典语句24：2030 年的时候，我们能不能在军事变革方面追上世界。因为，没有工业基础、技术基础、经济基础，拿什么追呢？战争打的就是工业，就是技术啊！我们的是大泡，人家的是大炮！ 　　 <br />
　　经典语句25：到2030 年，人家是新型武器武装的新型军队，我们是新房子、新城市，正好做为新屠场，新坟墓！ <br />
　　经典语句26：美国不怕中国全民皆兵地保卫国土，因为它根本就不打算占领中国。 　　 <br />
　　经典语句27：中国与美国的关系，绝不仅仅是双边关系。美国拼命地搞中国，并不是因为中国天然和美国有仇，而是美国的战略目标需要。狼跟羊有什么仇？就是正常的吃和被吃的关系。它要舒服，你就必须倒霉。列宁说，帝国主义就意味着战争。只要美国不改变全球帝国战略，中国面临的威胁就是永存的。 　 <br />
　　经典语句28：在今天，战争绝不是传统意义上的概念，不再是两军对垒，刀光剑影。如果不得不兵戎相见，也只是斩首行动和战略瘫痪。从军事战略的目的上说，美国全面包围中国，并不是想直接进攻中国，而是想诱使中国把国防经费用在毫无用处的防卫本土方面，以免挑战美国赖以维持世界霸权的海空优势。 　　 <br />
　　经典语句29：中国之所以没有特别强的危机感，一是我们到处喊和谐、和平，这本来是说给世界听的，却把自己的人们麻醉了。大家谁也不愿意想战争了。前不久在《环球时报》的那个关于未来十年发展的研讨会上，除了我跟王小东，谁都不提战争。复旦大学的沈丁力先生还从很高深的角度，论述了中国未来不会有战争。《环球时报》和稀泥，把我关于中国已被包围的文章跟他放一起发，交给中国人民自己去判断。 　　 <br />
　　经典语句30：可以看到，*力量，香港*派，*功；*分子，*集团、热比娅集团，总后台无一不是美国，无一不是接受美国的政治、军事、舆论和经济支持。而美国支持这些中国分裂力量的目的，也只有一个，那就是让台湾和香港，不要融入大陆的统一发展进程，让大陆继续分裂，由外向内，最后**。 　　 <br />
　　经典语句31：在中苏对峙的时候，我们是把东北部看做中国头部的，把西部看着中国的尾部。但是，现在，我觉得应该反过来看，西藏是我们的头部，新疆是我们的咽喉，东南沿海是我们的心腹，南海是我们的爪子。本来琉球还有一只爪子，清朝的时候，被日本砍掉了，现在也没有收回来。西藏为什么是我们的头部？因为我们的太空基地和很多战略设施都在这里。未来军事竞争、技术竞争，就是玩太空。没有太空工业，就没有国家的未来。 　　 <br />
　　经典语句32：新疆呢，是中国从中亚到内地的石油和天然气通道。美国为什么支持*和热比娅？就是放长线钓大鱼，有朝一日，用这两把刀，斩首中国，当然，握这两把刀的手，还是美国的手。美国不是想不想的问题，是时机成熟不成熟的问题。 　　 <br />
　　经典语句33：让我格外忧心的是军队改革进程太慢了。除了*时期的改革，之后就只是换了几次军装而已。新型军队的口号，就我一个人在提。用”鹰击战略“取代”刺猬战略“的口号，就我一个人在喊。转型要从陆军形态向海空复合型转型也我一个人在提。我这么小的官，有什么用呢？ 　　 <br />
　　经典语句34：我总是觉得今天的中国和拿破仑死后的法国一样，当时的法国，民族没有了灵魂，国家没有了方向，军队不会打仗，也不敢打仗了。先是一连串的失败，到二战时干脆全军缴枪了。今天中国呢，毛泽东死后也差不多是这个状况。 　　 <br />
