													<h2>易中天：中国梦：梦与梦魔</h2>
						
						
																											<div id="postmessage_772834" class="t_msgfont">根据现场录音手打，稍微删掉了一些口水话，同时参考了易中天的见报稿（当然删了很多内容）和现场记录稿（不完整）。<br />
<br />
--------------<br />
<br />
2010年8月1日<br />
<br />
尊敬的龙应台先生，尊敬的各位来宾，欢迎大家来到北大，参加《南方周末》举办的“中国梦”系列讨论，在昨天的中国梦致敬盛典里面，我们的致敬嘉宾代表大家向龙应台先生表示了致敬。我现在谨以以我个人的名义，代表我自己向龙先生致敬。我的致敬词是一幅对联。<br />
<br />
&nbsp; &nbsp; * 上联：1949龙应台大江大海<br />
&nbsp; &nbsp; * 下联：2010易中天小打小闹<br />
<br />
我今天小打小闹的题目是《中国梦：梦与梦魔》<br />
<br />
我先给大家讲一个故事。1793年，英王乔治三世派出马戛尔尼勋爵率团来华。表面的目的是为乾隆皇帝补贺八十大寿，实际上真正的目的有两个：一是通商，二是传教。戛尔尼浩浩荡荡的代表团来到中国之后，他们遇到了一个麻烦――官方坚持他们见到皇帝时要行三跪九叩之礼。马戛尔尼说：行礼没有问题，但是根据对等原则，中方必须派和他们特使同等官阶的大臣，向英王乔治三世的画像行三跪九叩之礼。这个要求当然是大清王朝断然不能接受的，于是乾隆指示下属对马戛尔尼“详加开导”，在这个开导当中，乾隆皇帝表现出天朝大国的天子雍容大度，乾隆皇帝说：“朕很体谅你们，英夷不远万里来到中国，不容易。朕也体恤你们洋人有打绑腿的习惯，跪不下来不要紧。朕恩准行跪拜礼时解开绑带，拜完了朕还恩准你们再穿上，这不是挺方便吗？有什么不可以？”。这次出使的结果是不欢而散。那么为什么呢？为什么当时的中国当局要坚持行三跪九叩大礼呢？很简单，在当时中国人的概念中我们是“天朝”，天朝的天子是“万民之主”，所谓“普天之下，莫非王土，率土之滨，莫非王臣”，英国不过是一个番邦，尔国国王来此亦要躬行此礼，何况你一小小陪臣？<br />
<br />
<br />
这就是当时的中国人的中国观，他们的中国观和世界观是统一的，只有一个天下，也只有一个天子。这种中国观和世界观在鸦片战争中就被西方列强的炮火炸得粉碎。鸦片战争以后，《南京条约》里面特别有一个条款，以后两国公文来往应称为“照会”。这不是外交惯例么？以前是不知道的，那以前叫什么呢？以前叫“上谕”。我甚至猜测，他们当时是不是还要派一个大太监，过来给外国使节们“下旨”：“奉天承运皇帝诏曰：尔等不远万里，十分辛劳，赐烤鸭一只，钦此――”<br />
<br />
这说明什么，说明我们中国人是通过签订不平等条约学会了与别人平等交往，但我们学会平等交往接下来的屈辱是割地赔款、丧权辱国。对中国人刺激最大的是两件事请，一是甲午中日战争，我们居然被一贯瞧不起的蕞尔岛夷――“倭寇”给打败了；二是巴黎和会，甲午中日战争毕竟我们是战败了，只能含着眼泪签马关条约。可是一战我们是战胜国啊！战胜国在巴黎开会，巴黎和会上把国家分为三等，不同等级国家派的代表数量有差别，一等国五人，二等国三人，三等国两人。中国政府想都没有想就派了五个代表过去，到那儿一打听才发现原来是三等国。最后没办法，只好五个代表轮流开会。我们连巴西、比利时和塞尔维亚都不如，人家一开始也被划分到三等国，后来经过“努力争取”，变成了二等国。这两件事给当时中国人的刺激太大。从此我们告别了天朝大国，中国中心的这样一个观点。告别了过去的那种“天下梦”――“中国梦”开始了。<br />
<br />
<br />
“中国梦”的主题当然是“强国”。因为我们受欺侮，我们被蹂躏、我们被歧视。我们必须使自己的祖国强大起来，至少要能够理直气壮的自立于世界民族之林。问题是怎么强国？什么叫强国？什么叫强大的国家呢？非常值得庆幸的是，晚清时代我们当时的一些先驱，一些有识之士，对此有不俗的理解，这些人包括徐继畲、郭嵩焘、谭嗣同等等。他们共同的观点是：一个真正的强大的国家，它的标准绝不仅仅是船坚炮利，也绝不仅仅是财大气粗，首先是政治文明，道德高尚。只有政治文明，道德高尚才配称为强国，也只有政治文明，道德高尚才能够成为强国！<br />
<br />
问题是怎么办？先贤们给出的办法是：像西方人一样实现儒家的政治理想。为什么会有这样的观念？他们有这样的共识：世界上最文明最先进的国家，三代之前是中国，此刻是欧美。为什么？ ――因为我们祖先，我们的先哲们、先人创造的文明，先进的理念被败家子给丢光了，反倒是欧美这些国家把我们这些东西发扬光大了。<br />
<br />
&nbsp; &nbsp; * 西方的“虚君共和”，不正是我们孟子的“民贵君轻”么？<br />
&nbsp; &nbsp; * 西方的民主制度，不正是《礼记》里面的“选贤任能”么？<br />
&nbsp; &nbsp; * 西方议会里的议员，不正是我们“为民请命”的乡绅么？<br />
&nbsp; &nbsp; * 美国的那个华盛顿，不正是我们“禅让天下”的尧舜么？<br />
<br />
你看，我们的好东西都到他们那里去了！<br />
<br />
当时说英国是“仁爱兼至”以至于“寰海归心”，依我看这就有点走火入魔了。那不是靠仁政换来什么“寰海归心”，殖民地都是强抢过来的，美国屠杀印第安人，也不是什么“仁爱兼至”。还有人说西方人礼让，守秩序，秩序井然，彬彬有礼，这不就是中国传统的礼教么？错。西方人是礼让，守秩序，但是和中国不一样啊：西方人礼让那是“女士优先”，中国是“领导先走”！<br />
<br />
再说西方还有很多概念没办法套到中国的传统上来，那是西方自己创造的概念。<br />
<br />
&nbsp; &nbsp; * 三权分立对应什么？――三省六部？<br />
&nbsp; &nbsp; * 政党轮替对应什么？――改朝换代？<br />
&nbsp; &nbsp; * 宪法对应什么？――《四书》《五经》？<br />
<br />
总不可能最高法院判断是否违宪，法官去念“关关雎鸠”？另外参众两院，根本没有可以对应的。但是我们不应该苛求，更不能苛责先贤。我们应该看到，他们能够这样想问题，已经很不容易了。当我们今天向中国梦的践行者致敬的时候，请不要忘先贤，不忘记他们做过的努力。<br />
<br />
问题是何以，这让我想起马克思在《路易・ 波拿巴的雾月十八日》中写道：“人们自己创造自己的历史，但不能随心所欲的创造历史……一切已死的先辈们的传统，象梦魔一样纠缠着活人的头脑。……在这种革命危机时刻，人就就会战战兢兢地请出亡灵，穿着他们的衣服、拿着他们的武器，喊着他们的口号，演出自己的活曲。“所以，我们就要回头看，鸦片战争之前我们有什么梦？中国人有什么梦？――我的结论是鸦片战争前中国人的梦是“天下梦”。“天下梦”后来变成了“中国梦”，这两者的区别是：<br />
<br />
&nbsp; &nbsp; * “天下梦”想的是“人类怎么样”，“中国梦”想的是“中国怎么样”。<br />
&nbsp; &nbsp; * “天下梦”想的是“社会怎么样”，“中国梦”想的是“国家怎么样”。<br />
<br />
鸦片战争前这个“天下梦”的概念又分成两个，就是“大同”和“小康”。<br />
<br />
<br />
&nbsp; &nbsp; * “大同”是天下为“公”，“小康”是天下为“家”。<br />
&nbsp; &nbsp; * 在政权轮替和权力交接上，“大同”是禅让制， “小康”是世袭制。<br />
&nbsp; &nbsp; * “大同”和“小康”最重要的区分是看权力归谁<br />
这样看就很明白，大同比小康好，所以晚清的先贤们就认为，美国的共和联邦制，好于英国的虚君立宪制，又好于日本的实君立宪制。美国的共和联邦制度最接近中国人的“大同”理想。这当然是一部分人的观点，不是统治者的观点，清朝统治者的观点也很简单，最好什么都别变，万不得已要变就学日本，国家要有一个“万世一系、永远尊崇”的君主；维新派主张学英国虚君宪政，革命派就主张学美国共和联邦。<br />
<br />
实际上呢，<br />
<br />
&nbsp; &nbsp; * “大同”是我称之为由部落联盟向国家联盟过渡的阶段，是文明的开端；<br />
&nbsp; &nbsp; * “小康”是西周到春秋这段时期，周公的一个政治理想。就是由宗法、封建和礼乐构成的。宗法是社会制度，封建是政治制度，礼乐是文化制度，三位一体。要做到以人为本，以德治民，以礼为序，以乐促和。<br />
<br />
在儒家的眼里：<br />
&nbsp; &nbsp; * “大同”是“无忧无虑”<br />
&nbsp; &nbsp; * “小康”是“有德有序”<br />
<br />
所以孔子说“吾其为东周乎！”就是“大同”回不去了，退一步，回到“小康”总可以了吧？但是不可能。秦始皇一剑定乾坤，一统天下，兼并了其他的主权国家。建立了一个统一的王朝，从此小康也没有了，“百代皆行秦政制”，几千年都是郡县制的帝国主义。“无忧无虑”是指望不了了， “有德有序”也不行了，能过上安生日子也就不错了。<br />
<br />
那怎么办呢？开始做第三梦，“治世梦”。“治世梦”就是不要天下大乱，只要天下大治，标准是风调雨顺，国泰民安，政通人和。实现“治世梦”要靠谁呢？首先得靠老天爷实现“风调雨顺”，然后“国泰民安”和“政通人和”主要依靠三种人：圣君、清官和侠客。<br />
<br />
&nbsp; &nbsp; *最好是有一个好皇上，好皇上的标准是仁慈和明智，仁慈的叫“仁君”，明智的叫“明君”，又仁慈又明智的那就叫“圣君”了。<br />
&nbsp; &nbsp; * 没有“圣君”怎么办？打个折扣，我头顶上的父母官是“清官”也可以。<br />
&nbsp; &nbsp; * “清官”也没有，就只能靠“侠客”了。都希望侠客半夜入衙取贪官首级嘛。<br />
<br />
所以你看为什么《三国》中的尊刘贬曹大快人心？在刘备集团里面圣君、清官和侠客都齐了。刘备是圣君，诸葛亮是清官，关羽张飞是侠客。“三个代表三个梦”。但是往往就连“侠客梦”都做不成了怎么办呢？只好读武侠小说，所以说金庸小说大行其道。这叫作越来越没有指望。而且我们可以看出来，从大同梦到侠客梦，这三个梦是王二小过年，一年不如一年。<br />
<br />
到了1949年，有了一个新版本的中国梦，这个版本的主题仍然是“大同”，标志性的表现是“人民公社”。人民公社这个东西――哦，不是东西。嗯，人民公社这个 “新生事物”，它即使不是按照大同社会设计的，但它很符合“大同”的精神。你看大同是<br />
<br />
&nbsp; &nbsp; * “财货不必藏于己”――猪啊羊啊都交公了，<br />
&nbsp; &nbsp; *“劳动不为己”――集体生产不为己<br />
&nbsp; &nbsp; * “老有所终”――有敬老院，<br />
&nbsp; &nbsp; *“壮有所用”――公社社员都干活<br />
&nbsp; &nbsp; * “幼有所长”――有托儿所，<br />
&nbsp; &nbsp; * “鳏寡孤独废疾者皆有所养”――有五保户。<br />
&nbsp; &nbsp; * “故人不独亲其亲，不独子其子”――集体生活<br />
&nbsp; &nbsp; * “路不拾遗，夜不闭户”――东西都交公了，自己什么都没有，闭什么户啊？<br />
<br />
人民公社的杰出代表就是大寨，在当时人们心目中那就是“人间天堂”。当年有一首歌，我也会唱，讲大寨的，是这么唱的：<br />
<br />
一道青河水，一座虎头山，大寨就在这山下边。七沟八梁一面坡，层层梯田平展展，牛羊胖乎乎，新房齐崭崭，炕上花被窝，屯里粮满尖。银光闪闪喜气多，社员梦里都笑声甜。<br />
<br />
要是过上这个生活，那真是“社员梦里都笑声甜”啊。不过后来我一想，这歌词里唱的不就是我们现在要建设的“社会主义新农村”么？这个梦确实是“中国梦”，但它的源头不是儒家，更多是来自墨家。墨家最重要的思想是两个，一是“兼爱”，一是“尚同”。<br />
<br />
&nbsp; &nbsp; * 在生产分配方面，讲究自食其力，按劳取酬；<br />
&nbsp; &nbsp; * 在人事制度方面，讲究机会均等，能上能下；<br />
&nbsp; &nbsp; * 在社会关系方面，讲究互敬互爱，平等兼爱。<br />
&nbsp; &nbsp; * 在组织纪律方面，讲究上之所是，必亦是之；上之所非，必亦非之。就是村民意见不统一，就听村长的；村长意见不统一，就听乡长的；乡长意见不统一，就听大夫的；大夫意见不统一，就听国君的；国君意见不统一，就听天子的。这是“民主集中制”。<br />
<br />
所以人民公社思想更多来自墨家。墨子自己，粗茶淡饭，草鞋布衣，劳作不止。他的学生，更是晴天一身汗，雨天一身泥，手脚长满老茧，肌肉鼓鼓，面黑如炭。这墨者不就是当时的“知识青年”么？到了汉代“罢黜百家，独尊儒术”以后，儒家就变成了“公开的执政党”，法家是“暗中的执政党”，道家是参政议政的“在野党”，墨家变成了“地下党”。<br />
<br />
农民出身的领导革命的毛泽东对墨家思想情有独钟并不奇怪，奇怪的是他自己不这么说，他说自己是“马克思加秦始皇”，是社会主义+法家思想，那这就跟晚清诸贤不一样了，他们是资本主义+儒家思想。这个事情是很奇怪的，因为晚清诸贤包括谭嗣同，都对法家、秦始皇是恨之入骨的，谭嗣同说得很尖锐：”二千年之政，秦政也，皆大盗也；二千年之学，荀学也，皆乡愿也“。从秦汉到明清，中华大地上盛行的就是大强盗统治下的伪君子。谁造的？罪魁祸首是秦始皇、法家。<br />
<br />
毛泽东是谭嗣同的湖南老乡，年轻的时候是他的粉丝，他不会不知道这个。何况，这个法家是没有大同梦的，毛泽东他要做大同梦为什么要去找法家呢？我的理解是这样的，毛泽东他有两个梦，“大同梦”和“强国梦”。“大同梦”更多来自墨家，“强国梦” 就要靠法家来实现，因为法家是讲斗争的。先秦诸子，儒墨道法。墨家讲究逻辑，儒、道、法讲究矛盾。而讲究矛盾的里面，儒家讲究矛盾的统一中和，道家讲究矛盾的转化，只有法家讲的是矛盾的斗争。法家的哲学就是斗争的哲学，毛泽东的哲学也是斗争的哲学。很对他的思想，也很对他的胃口，因为毛泽东说的很清楚：” 与天斗其乐无穷，与地斗其乐无穷，与人斗其乐无穷“，他喜欢这些。<br />
<br />
而且，能跟马克思和恩格斯的某些说法接上。马克思和恩格斯在《共产党宣言》里说，一切社会的历史，都是阶级斗争的历史。那么马克思的阶级斗争思想和法家斗争哲学结合在一起，这就叫做马克思+秦始皇。于是就有了文化大革命，文化大革命就是这种思想的实现，结果众所周知。强国梦没有实现，国民经济到了崩溃的边缘。大同梦也没有实现，整个国家遭受了空前的破坏和摧残。<br />
<br />
当然，我们不能以成败论英雄，但是我们不能不反思教训。教训在哪里？我们来看看人民公社和文化革命。人民公社的特点，是“一大二公”。文化革命的口号是“破私立公”。大同梦的口号是天下为公。一大二公，破私立公，天下为公，一以贯之的，不就是一个“公”字吗？但“大同梦想”的“公”指的是政权乃天下之公器，是讲公权力，而毛要做的事情是要把财产甚至精神都要充公，都要公有化。这就不是马克思和恩格斯的想法，在《德意志意识形态》中，马克思和恩格斯宣称，任何人类历史的第一前提，就是“有生命的个人的存在”。《共产党宣言》里面这样说：共产主义社会“将是这样一个联合体，在那里，每个人的自由发展是一切人的自由发展的条件。”因此按照马克思和恩格斯的观点，没有个人，没有个人的权利和自由，就没有共产主义。因此这种破私立公的思想不是马克思和恩格斯的思想，是法家的，法家的思想很清楚，一个国家，除了君主和官吏，只能允许两种人存在，这就是农民和战士。因为农民可以种田，战士可以打仗。显然，在他们眼里，人民不是个人，是工具和武器。工具和武器，又要什么权利，讲什么自由？<br />
<br />
这大概是我们文化背景的原因。中国文化从来都是把集体放在第一位的，个人放在第二位的。有一位叫做金观涛的学者曾经做过专门的检索，“民主”“共和”这样的概念很 早就被引入中国，被中国人所接受，因为这些概念可以和我们的大同思想接轨。但是“个人”“个性”这样的词直到“五四”以后才被介绍到中国。这样一来就有一个非常有意思的事情，晚清诸贤最推崇的，最接近大同社会的美国联邦制，在西方国家中是最个人主义的。没有他们的个人主义就没有他们的独立宣言，没有他们的独立宣言，就没有他们的联邦宪法，就没有真正意义上的美利坚合众国。个人，个人的权利和自由，难道是可以小看的吗？<br />
<br />
其实如果说起我们传统观念中最早主张个人的权利和自由的，我觉得是道家的杨朱――这里要做一个植入广告，你们可以去看我写的《我山之石》，那里面我 提出了一个观念，就是杨朱的理论是中国历史上第一部《人权宣言》。道家也讲“大同”，但与儒墨两家有别。儒家的“大同”，是领导海选、权力禅让的“尧舜之世”。墨家的“大同”，是村长领着大家干活，有意见逐级向上反映的 “人民公社”。道家的“大同”，却是不要领导，或形同虚设。人民自由自在，无拘无束，想干嘛干嘛，爱怎么过就怎么过。用庄子的话说，就是“上如标枝，民如野鹿”。这样，同样是“大同梦”，道家的是个人主义，法家的是集体主义，儒家的是折衷主义。这叫做“一个梦想，各自表述”。<br />
<br />
说到这里我想给大家说一个我年轻时候的事情。以前中学校园流行喇叭裤，就是上窄下宽的设计，然后当时有个说法叫“问题青年”，形象就是穿喇叭裤，戴蛤蟆镜，手里提着一个收录机，一边走一边放邓丽君的歌：“路边的野花，你不要踩~~~”（笑声）。那个时候学校的老师也是非常郑重的做思想政治工作的，看到学生穿喇叭裤，品行不端，不能不管啊，他们就贴了一个标语：“喇叭裤能吹响向四个现代化进军的号角吗？”然后有学生在底下贴了一个条： “请问什么裤子吹得响？”<br />
<br />
这事发生在改革开放之初，听说的人也大多一笑了之。但十年以后，却有了别样的意义。因为改革开放头十年，胆子最大，步子最快，思想最解放，成就最显著，以至于全国人民争相学习仿效的地方，就是最先穿喇叭裤的地方。这当然不能归功于喇叭裤，却应该归功于广东对个人权利和个体自由，给予了最大限度的尊重。直到现在，广东也仍然是舆论最新锐、环境最宽松的地方。这才有了深化改革、扩大开放的“广东经验”，有了关注民生、自由言说的“南方视角”。就没有这样的“南方视角”。没有这样的“南方视角”，就没有现在的南方报业集团，也没有现在的《南方周末》！（掌声）<br />
<br />
于是我们发现，尊重个人权利和个性自由，（加强语气）离“大同”最近！（掌声）<br />
<br />
但这还不是最重要的。更重要的是，我们为什么要有“中国梦”，我们又为什么要“强国”？<br />
<br />
国家富强，是为了人民幸福，而人民是由无数个“有生命的个人”组成的。没有每个个人的幸福，就没有人民的幸福。没有每个个人的未来，就没有人民的未来。这就首先要让全体国民，都成为“人格独立，意志自由”的人。我们说要建设一个强国，那么这个强国里，我话不能随便说，衣服不能随便穿，饭不能随便吃，这样的强国我要它干什么！把这些“个人”都变成既没有“独立人格”，又没有“自由意志”的齿轮和螺丝钉，那么，组装起来的，将是一架没有人性的机器，一架杀人的机器，毁灭人类的机器。在这样思想下建立起来的秦王朝，它二世而亡，活该！<br />
<br />
我们改革开放30年来最大的进步，就是我们有了选择的自由。农民可以进城打工，工人可以下海经商，大学生可以自主择业，所有人都可以跳槽。当然，这些选择，未必都成功，也未必都主动。但能够选择，敢于选择，就是成功。我们能选择了，也敢选择了。我们经历了两次解放的过程，第一次是“五四”，让中国人可以自由恋爱，“我的恋爱我做主”；后来是改革开放，让中国人可以自由择业，“我的职业我做主”。这两个“我做主”，背后体现的都是这样一个原则：我是公民，是独立的个人。我的权利我主张，我的事情我做主！<br />
<br />
与此同时，中国梦，也发生了深刻的变化。就是可以不再集体做梦，可以不在同一个体制下，同一个框架里做梦，可以随便做梦，想怎么做梦就怎么做梦，甚至可以同床异梦。<br />
<br />
我们中国人，几千年来，做了三个梦：天下梦，国家梦，个人梦。天下梦就是大同梦，国家梦就是强国梦，个人梦就是幸福梦。这些梦是互为前提，彼此成全的。社会不稳定，个人难发展；国家不强大，个人没前途。但，社会的进步，国家的富强，又归根结底是为了每个人的幸福，为了每个人的全面自由发展。背离了这个目标，则一切均无意义。天下梦，中国梦，个人梦，岂非互为前提，彼此成全？中国的道路这就只能靠我们共同努力了。实际上，中国道路从来就不是一个理论问题，而是实践问题。实现“中国梦”，既要追求，又要反思，更要实践。只要有越来越多的践行者，只要有越来越多的人加入这个行列，我们就总有一天能够实现自己的梦想──社会进步、国家富强和个人幸福。<br />
<br />
这个梦可以实现。<br />
<br />
这个梦应该实现。</div>

							
							
							
							
