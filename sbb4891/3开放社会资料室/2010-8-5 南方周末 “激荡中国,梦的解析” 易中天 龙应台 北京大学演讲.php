																											<div id="postmessage_770987" class="t_msgfont"><strong>易中天：人民不幸福，国家再强大有什么用？</strong><br />
<br />
易中天认为，国家的强盛需要众多自由的个人来建立，而那些忽视个人自由所组建起来的集体只能是一部冰冷的机器。他举例说，上世纪八十年代初，大学生穿喇叭 裤都引起老师和校长的非议，认为这是品行不端的表现。易中天说，这就是典型的干涉个人自由的表现。一个国家再强大，可底下的人吃不饱饭，没有说话的自由， 没有穿喇叭裤的自由，请问我们要这么强大的国家有什么用呢？<br />
<br />
其实，易中天的这话是对早年胡适的名言进行新的注解。胡适曾说：“现在有人对你们说‘牺牲个人的自由，去求国家的自由!’我对你们说：‘争你们个人的自由，便是为国家争自由!争自己的人格，便是为国家争人格!自由平等的国家不是一群奴才建造得起来的!”<br />
<br />
易中天为此对不重视个人自由的秦王朝的灭亡幸灾乐祸：秦始皇本来想让子子孙孙万万世世为王，可就在秦二世手里因残暴统治被人民给推翻了。这样的王朝被推翻真是活该！！！易中天说“活该”的口气是恶狠狠的，顿时台下掌声雪花般飞来。<br />
<br />
在此，本人阐发两句，早年间面对国家自由和个人自由时，鲁迅主张牺牲个人自由成全国家自由，而胡适主张先个人自由再为国家争取自由。而我们长期以来就是在鲁迅的这种思想下被灌输的，现在看来这是一条弯路，在这方面胡适的先见之明远远高于鲁迅。<br />
<br />
<strong>龙应台：国家的崛起，首先是文明的崛起</strong><br />
<br />
龙应台说，她也是盼着中 国崛起，但中国的崛起首先是文明的崛起。相比于文明的崛起，军队的崛起和经济的崛起都是次要的。如何评判“文明”的定义，龙应台说她有一把尺度：就看这个 国家能否真正体恤弱势群体，能否包容不同的异议分子，如何面对国内的少数民族，如何让国内的大多数面对少数人。她还指出，两岸的统一首先要从（政治）文明 为切入点，寻找共同的价值观实现民族和解。<br />
<br />
龙应台先生的这番话对我很有感触，看看周边的人，他们议论国家崛起常常限于军事和经济崛起：中国今天能打败这个国家了，明天能打败那个国家了；中国的经济总量前几年超过德国了，今年超过日本了。而他们很少意识到中国的政治文明与其它国家的巨大差异。<br />
<br />
本人以为，百姓有选票是政治文明的第一步，然后积极培育国家的公民精神，否则，即便有选票也容易陷入多数人的暴力统治中。我不知道中国的政治文明目前进程到了哪个阶段。<br />
<br />
龙应台说，她相信大陆内部的一部分人在为政治文明的崛起默默地奋斗着，因为有些价值观是人心所向的。（本人注：连温家宝总理都认为，民主自由乃是普世价值。）<br />
