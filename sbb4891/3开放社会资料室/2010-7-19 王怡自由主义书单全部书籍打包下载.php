																											<div id="postmessage_758327" class="t_msgfont">[商务印书馆汉译学术名著丛书] <br />
1、孟德斯鸠《论法的精神》：孟德斯鸠和贡当斯是法国思想家中难得的两个在思想气质上偏向英美思想的人，这本书是整个近代文明中基础性的学术著作。 <br />
2、卢梭《社会契约论》◆ <br />
3、卢梭《论人类不平等的起源与基础》◆：尽管卢梭的思想近来多被指责为法国激进主义的源头，但正如有人指出，我们与欧陆思想之间的差距，其实远远大于欧陆与英美之间的思想差距。这两本书无论如何也是基础性的。 <br />
4、洛克《政府论》 <br />
5、洛克《论宗教宽容》：作为英式启蒙的代表人物，现在洛克比卢梭自然更受关注。洛克的社会契约论与卢梭的社会契约论之间路数的差异，对我们来说是一种奢侈的差异。 <br />
6、梅因《古代法》：在这本书中梅因提出了一个传统向现代转化的著名公式：从身份到契约。这是从法律角度对现代自由主义的最佳诠释。 <br />
7、霍布斯《利维坦》：一个非自由主义的国家，就是一只巨大的怪兽。 <br />
8、密尔《代议制政府》 <br />
9、密尔《论自由》：绝对的经典著作。 <br />
10、《拿破仑法典》：即1804年法国民法典。民法及其基本价值取向在本质上就是自由主义的法律体现。拿破仑法典是一个辉煌的起点。 <br />
11、亚当斯密《国富论》 <br />
12、亚当斯密《论法律、警察、军事、岁入》：经济自由主义的鼻祖。 <br />
13、托克维尔《论美国的民主》◆ <br />
14、托克维尔《旧制度与大革命》：对美国民主和法国革命的经典论述。 <br />
15、霍布豪斯《自由主义》：最早的完整表述。 <br />
16、边沁《政府片论》：功利主义和经验主义的早期大师 <br />
17、《联邦党人文集》◆：非看不可的自由主义政治实践文献。 <br />
18、《潘恩选集》：我对潘恩的了解最初来源于朱学勤先生的文集《风声、雨声、读书声》。 <br />
19、曼海姆《意识形态与乌托邦》 <br />
20、边沁《道德与立法理论导论》2000年 <br />
21、韦伯《新教伦理与资本主义精神》三联版、四川人民版：不用说了。 <br />
22、韦伯《论经济和社会中的法律》大百科版 <br />
23、弗洛姆《逃避自由》工人出版社：对于法西斯下社会心理的分析，是我把他看作对自由主义的一种反面认识。 <br />
24、马尔库塞《单向度的人》重庆人民版 <br />
25、马尔库塞《理性与革命》重庆人民版：对资本主义的文化批判，是什么促使了一个单向度的社会？而一个马尔库塞所描述的单向度的社会绝对是与自由主义为敌的。这是西马站在左倾立场对于自由主义的早期挑战。 <br />
<br />
26、波普尔《历史决定论的贫困》华夏出版社&quot;21世纪文库&quot;（中社科2000年《历史主义的贫困》） <br />
27、波普尔《开放社会及其敌人》中社科2000年&quot;西方现代思想译丛&quot;：对于乌托邦思想最彻底的清算来自波普尔和哈耶克，这本书无愧于自由主义巨作。 <br />
<br />
28、罗尔斯《正义论》中社科&quot;外国伦理学丛书&quot;：虽然这本书以&quot;正义&quot;这样的宏伟叙事作为立论的基础，是对于经验主义的英美传统的某种背叛，所以罗尔斯本人被称为是自由左派，导致了诺齐克的思想反击。但按照哈耶克的理解，罗尔斯与他的差别其实只是叙事修辞上的。 <br />
29、罗尔斯《万民法》 <br />
30、诺齐克《无政府、国家与乌托邦》：自由右派对于《正义论》形而上倾向的坚决回应。著名的&quot;自愿为奴的&quot;质问就出于此书。 <br />
<br />
31、哈耶克《个人主义与经济秩序》北京经济学院出版社&quot;诺贝尔经济学奖丛书&quot;1991 <br />
32、哈耶克《通往奴役之路》中社科&quot;现代西方学术译丛&quot; <br />
33、哈耶克《致命的自负》同上。 <br />
34、哈耶克《自由秩序原理》三联&quot;宪政译丛&quot; <br />
35、哈耶克《法律、立法与自由》大百科&quot;外国法律文库&quot; <br />
36、霍伊《自由主义政治哲学》三联新知文库：对哈耶克政治学思想的最好入门读物。 <br />
<br />
37、里夫金《熵，一种新的世界观》◆上海译文&quot;当代学术思潮译丛&quot;1987：我之所以将这本曾经轰动一时的通俗学术著作列入，因为这种新的世界观是对历史目的论和乌托邦思想最好的解构。 <br />
38、房龙《宽容》◆：胡适曾说，&quot;宽容比自由更重要&quot;。这本书是关于一个自由主义者基本的思想秉性的最好的通俗读物。 <br />
<br />
39、伯尔曼《法律与革命》大百科&quot;外国法律文库&quot;：梁治平先生曾经非常推崇这本书，如同新教伦理与资本主义精神一样，他指出了现代法治文化与基督教之间的渊源。 <br />
40、德沃金《认真对待权利》大百科&quot;外国法律文库&quot;：著名的自由主义法学家。 <br />
41、庞德《普通法的精神》法律版：对英国法律价值观的简要分析和总体把握。 <br />
42、巴顿卡特《大众传播法概要》中社科1997&quot;美国法学精选&quot;：对言论与新闻自由的法律把握。 <br />
<br />
