													<h2>刘慈欣：《死神永生》完成</h2>
						
						
																											<div id="postmessage_809724" class="t_msgfont"><a href="http://blog.sina.com.cn/s/blog_540d5e800100lfwn.html" target="_blank">http://blog.sina.com.cn/s/blog_540d5e800100lfwn.html</a><br />
<br />
<br />
<br />
<br />
36万字。<br />
<br />
至此，《三体》系列全部完成，共三部，88万字，算是很长的一本书了。<br />
<br />
最近，几乎每天都收到电邮、短信和电话，催第三部，时间长了，感觉自己成了欠一屁股债的杨白劳，耳边不时响起黄世仁的声音：大刘啊，大年三十帐不过夜，拿闺女抵债吧。<br />
<br />
其实这本书写的不算慢了，《黑暗森林》2008年出版，以后有一年多的时间因外部原因没有写作，第三部也就是写了一年左右。就是算上没写的时间也不算长，36万字的一部长篇，要保证质量，怎么也需要三四年的时间。<br />
<br />
我们一直在呼唤精品，可精品是怎么来的？以长篇小说而言，首先需要时间。当然，肯定有那样的天才，漫不经心随手涂鸦，即成旷世经典，但这样的人肯定百年难遇。像我们这样的普通作者，写长篇小说是需要很长时间的，要一砖一瓦构建一个世界，要与自己的心灵进行漫长的对话。<br />
<br />
但即使没有读者的催促，我也不可能花三四年写一部长篇。这是一个急功近利的时代，笔者也不能免俗。现在，耐的住寂寞的作者，同坐怀不乱的男人一样，不能说没有，但极其罕见。<br />
<br />
想快些写还有一个原因，就是一种危机感。我感觉自己的创作就像一个导游，带着读者去游览自己的想象世界。我带的这个团已经转了十余年，可到现在连一半的景点都没转完，还不算那些新开发的景点。所以心里总是有些焦虑。因为我知道，意外随时都可能出现，洪水可能挡住我们的路，我们的大巴可能被枪手劫持。作为一个老科幻迷，我知道这不是闳擞翘臁<br />
<br />
在国内科幻可能遇到的种种意外中，最让人担心的就是可能出现的社会动荡。在这次笔会上，我对读者朋友说科幻是一种闲情逸致的文学，他们都不以为然，但这是事实。只有在安定的生活中，我们才可能对世界和宇宙的灾难产生兴趣和震撼，如果我们本身就生活在危机四伏的环境中，科幻不会再引起我们的兴趣。事实上，中国科幻的前三次进程中的两次，都是被社会动荡中断的，社会动荡是科幻最大的杀手。现在，平静已经延续了二十多年，感觉到在社会基层，有什么东西正在绷紧，压跨骆驼的最后一根稻草随时都可能出现。但愿这只是一个科幻迷的闳擞翘欤但愿太平盛世能延续下去，那是科幻之大幸。<br />
<br />
但不管怎么样，我们在科幻世界中的行程还是应该快些，在这第二人生中，我们应该及时行乐。引用《死神永生》中的一句话：那一刻，沧海桑田。</div>

							
							
							
							
