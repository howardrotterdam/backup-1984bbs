<br />
朝鲜领导人金正日秘密进入中国访问，官方强调致力于同朝鲜发展睦邻友好合作关系。就在金正日的专列还在进入北京的路上，中国网民发出“金正日滚出中国”的口号。<br />
<br />
据韩国媒体报道，朝鲜领导人金正日星期一乘坐专列秘密进入中国境内，并在星期二逗留大连，星期三到北京，这是金正日四年来首次访问中国。尽管中国官方发言人姜瑜星期二并没有承认金正日访华，只是一如既往地表示，“中朝是友好近邻，我们致力于同朝鲜发展睦邻友好合作关系”。而包括中国媒体在内已经普遍报道金正日在中国。《路透社》星期三消息，金正日专列已经抵达天津，并到港口做参访。<br />
<br />
星期二开始，中国网络上出现了“让金正日滚出中国”的行动，发起此行动的网友“张书记”星期三向本台表示，“其实就是一种宣泄，我觉得中国网民的宣泄口太少了，宣泄宣泄就被审查，被删贴，在推特上这种宣泄会好一些。其次我们也想如同自由国家的人们一样，可以去反对，可以去喊口号，去站大街。我们没有哪个权利，只能在网络上发起。”<br />
<br />
大批网友陆续开始加入“金正日滚出中国”行动。网友们批评金正日入住的的大连富丽华酒店总统包房，不但每晚价格高到1万6千元人民币，几乎相当于朝鲜的人均年度GDP总额，而且由于是专列和加长车队跟随，耗资巨大。有网友说，这些花费除了是朝鲜人民的民脂民膏，更可能来自于中国的援助。<br />
<br />
厦门网友郭宝峰用中英文在个人推特频道上说，“金正日，滚，滚，滚”。他告诉本台记者，“我自己非常 赞同这种做法，在网络上开展快闪行动，能够表达自己看法的活动，所以我就积极参与了，因为的确我也对金正日非常讨厌。而且这些行动其实可以让人知道，中国网民其实对金正日非常反感，他们的宣传是非常不要脸，颠倒黑白。”<br />
<br />
据新加坡《联合早报》星期三报道，金正日这次访华，双方讨论的重点应当是，中国向朝鲜提供多少援助，以及朝鲜如何回应国际社会重开六方会谈的要求。报道说，朝鲜连年粮食歉收，金正日访华的最重要目的肯定是粮食援助，还有经济制裁下也需要能源，包括石油等。今年2月，中国宣布决定对朝鲜投资超过700 亿元，并希望朝鲜回到六方会谈的谈判桌上。<br />
<br />
韩国庆熙大学国际关系教授朱载宇称，只要韩美、美日同盟存在，中朝同盟就将存在下去，朝鲜依然是中国的特殊盟国。<br />
<br />
对此 ，一直关注朝鲜问题的香港时事评论员李国成向本台表示，“金正日经过这么长的时间，好像基本上是同意要回到谈判桌来，但是他还有一些条件尚未得到中共高层的同意和保证，所以他这次又来了，和中共高层见面，而且还有一个要求就是要中共承认他指定的接班人，也就是他儿子，这次也可能带来了，要和中共接下来的接班人习近平见面。”<br />
<br />
