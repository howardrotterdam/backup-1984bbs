													<h2>新闻出版总署与文化部六十年恩恩怨怨</h2>
						
						
																											<div id="postmessage_291701" class="t_msgfont">走在北京宣武门外大街上，一幢正面形似电视屏幕、侧面如多列书籍组合的建筑显得格外醒目。这就是2008年年底投入使用的新闻出版总署新办公大楼。<br />
<br />
&nbsp; &nbsp; 总署新办公大楼在两届国务院主要领导同志的关心下，终于建成。这被看做是党和国家对新闻出版机构建设高度重视和支持的一个缩影。<br />
<br />
&nbsp; &nbsp; 新中国一成立就设新闻总署和出版总署<br />
<br />
&nbsp; &nbsp; 中华人民共和国成立后，根据《中央人民政府组织法》第十八条规定，政务院下设新闻总署和出版总署。1949年10月19日，中央人民政府委员会第三次会议通过任命：胡乔木为新闻总署署长，范长江、萨空了为副署长；胡愈之为出版总署署长，叶圣陶、周建人为副署长。<br />
<br />
&nbsp; &nbsp; 出版总署由华北人民政府教育部所属教科书编审委员会、中共中央领导下的出版委员会和新华书店编辑部三部分组合而成。<br />
<br />
&nbsp; &nbsp; 新中国出版事业犹如一张白纸，出版总署好比是工程师，要在这张纸上绘就新中国出版最美丽的蓝图，打好最坚实的基础。<br />
<br />
&nbsp; &nbsp; 从此，出版总署承担起领导全国出版事业的使命：提出了发展人民出版事业的基本方针，不遗余力地改进出版工作、书刊发行工作、期刊工作、书刊印刷业等工作，逐渐消除出版发行工作无组织、无计划的现象。领导制定《书稿报酬暂行办法(草案)》，筹建人民出版社以及新华书店总店等。开展读书运动，在工厂、农村、部队建立图书室。副署长叶圣陶还亲自主持，在出版总署和人民出版社共用的饭厅举办语法修辞讲座，这也是在中央新闻出版机关开展消灭错误运动的一项内容。<br />
<br />
&nbsp; &nbsp; 1951年10月12日，《毛泽东选集》第一卷由人民出版社出版。在庆祝会上，出版总署署长胡愈之介绍说，毛主席对自己的著作采取十分慎重的态度，几次亲自校阅修改。胡愈之号召以这次《毛泽东选集》的出版工作为标准，不断提高质量，做到十分认真严肃，没有任何错误。一年后，出版总署出台规定，提出每一书稿从采用到印制成书，应实行编辑初审、编辑主任复审、总编辑终审和社长批准的编审制度，以及编辑加工、设计、校对、印成后校读等基本程序。这也是新中国第一次明确提出出版社对书稿应实行“三审制”。<br />
<br />
&nbsp; &nbsp; 根据政务院的决定，1952年2月新闻总署撤销，原新闻总署各部门的工作陆续归并到别的单位。如原新闻总署国际新闻局的对外宣传、出版等工作，另设外文出版社负责办理，归并出版总署。<br />
<br />
&nbsp; &nbsp; 1954年11月16日，国务院发出通知：依照《中华人民共和国国务院组织法》第二条的规定，国务院不设立出版总署，出版总署所管理的出版行政业务划归文化部。<br />
<br />
&nbsp; &nbsp; 同年12月1日，文化部出版事业管理局正式设立。这个机构一直持续到1969年9月，后被国务院出版口取代。1973年9月26日，国家出版事业管理局成立。<br />
<br />
&nbsp; &nbsp; “文革”结束后，国家出版事业管理局首先在思想上进行了拨乱反正，使出版工作从“以阶级斗争为纲”的束缚中彻底解放出来，出版工作得到复苏和繁荣。<br />
<br />
&nbsp; &nbsp; 1978年10月11日~19日，国家出版事业管理局在江西庐山召开了全国少年儿童出版工作座谈会。1977年，全国出版的少儿读物只有192种，到了1980年就达到2000种，可以说和这次会议有很大的关系。<br />
<br />
&nbsp; &nbsp; 1982年5月5日，根据国务院部委机构改革的决定，原文化部、国家出版事业管理局、国家文物事业管理局、外文出版发行事业局、对外文化联络委员会5个单位合并，组成新的文化部。国家出版事业管理局为文化部出版局。<br />
<br />
&nbsp; &nbsp; 1985年7月25日，国务院批准文化部设立国家版权局，文化部出版局改称国家出版局，与国家版权局为一个机构两个牌子，仍由文化部领导。同年10月，国务院发通知，为了加强对全国出版工作的领导和管理，国务院决定将文化部所属国家出版局恢复为国务院直属机构。<br />
<br />
&nbsp; &nbsp; 1997年，组建新闻出版署<br />
<br />
&nbsp; &nbsp; 1986年12月上旬，正在家中整理东西的宋木文接到了中共中央任命他为国家出版局党组书记、局长，免去文化部副部长、党组成员职务的通知。还没来得及把在文化部分管的工作交接完，他便于12月中旬赶赴广西南宁主持召开早已定好的全国出版局(社)长会议。会议还没结束，北京来电话，中央决定撤销国家出版局，重新组建国家新闻出版局。<br />
<br />
&nbsp; &nbsp; 中央的这一决定，主要是基于当时的形势，提出要加强对新闻出版工作的管理、审批和检查。宋木文说，已成为“悬空”(局已撤销)的“空头”(只任命他一人)局长，受命组建新机构的工作。<br />
<br />
&nbsp; &nbsp; 宋木文想到了建国初期在中央政府设立有新闻总署、出版总署，他建议按这个名称和模式建立新闻出版总署。在讨论时，国家编委的同志提出，称“署”不称“总署”。当时的国务院秘书长陈俊生也同意不加“总“字，还说有“总”字的如海关总署、民航总局，都是中央对地方垂直领导的，新闻出版不属于这种体制。<br />
<br />
&nbsp; &nbsp; 1987年1月13日，国务院发出《关于成立中华人民共和国新闻出版署的通知》。新闻出版署为国务院直属机构，负责全国新闻出版事业的管理工作。国家版权局保留。这次机构改革，可以说奠定了今日新闻出版总署的基础。<br />
<br />
&nbsp; &nbsp; 沐浴着改革开放的春风，新闻出版署一手抓繁荣，一手抓管理，各项工作取得新进展。如举办国家图书奖、中国图书奖评选，催生出一大批优秀出版物。重视版权保护，1987年在青岛召开全国版权工作座谈会，研究确定地方版权管理机关的任务。1990年，《中华人民共和国著作权法》颁布，1991年开始实施。成立全国“扫黄”办公室，设在新闻出版署。<br />
<br />
&nbsp; &nbsp; 2001年，升格为新闻出版总署<br />
<br />
&nbsp; &nbsp; 新闻出版署的很多同志都不会忘记2001年，这一年有几件大事，让他们印象深刻。<br />
<br />
&nbsp; &nbsp; 那年两会举行的总理记者招待会上，朱F基总理提到了新闻出版署。<br />
<br />
&nbsp; &nbsp; 中央电视台记者问总理：“您对于目前政府机构改革和职能转变方面哪些进展比较满意，哪些方面还不太满意？哪些方面准备着力推进？”<br />
<br />
&nbsp; &nbsp; 朱F基回答：我认为本届政府的机构改革是成功的。我们还在进行改革，去年，已经把10个国家局，相当于过去的10个部撤销了9个，改革了1个。这就是很大的变化。同时，我们还要加强一些适应社会主义市场经济要求的部门，比方说国家工商行政管理局、国家质量监督局和国家出入境检验检疫局、新闻出版署，这些部门我们要加强、要升格，也就是说他们原来是副部级单位，现在要变成正部级单位。<br />
<br />
&nbsp; &nbsp; 2001年4月7日，新闻出版署召开署直系统干部大会，时任中共中央组织部副长的张柏林宣布了党中央、国务院关于新闻出版署升格为正部级单位，名称为“中华人民共和国新闻出版总署”，仍为国务院直属机构的决定。时任中央宣传部常务副部长刘云山参加会议并讲话。<br />
<br />
&nbsp; &nbsp; 刘云山说，这次机构升格的目的是加强新闻出版工作的领导和管理，增强工作的权威性，为新闻出版事业更大发展从组织上提供条件和保证。机构规格高了，地位变了，我们的责任更大了，任务更重了。<br />
<br />
&nbsp; &nbsp; 这一年的12月13日，国务院总理朱F基来到新闻出版总署考察工作。朱F基指出，党中央、国务院决定将新闻出版总署升格为正部级单位，这是深化政府机构改革、转变政府职能的重要步骤，新闻出版管理部门必须从思想观念、管理职能、任务重点和工作作风等方面有一个大的转变，要尽快实现由行业管理向社会管理转变，把主要精力放在对新闻出版市场的依法有效监管上。他还强调，要切实做好知识产权保护工作。要采取更加严厉的手段打击侵犯知识产权行为。<br />
<br />
&nbsp; &nbsp; 2009年1月16日下午，中共中央政治局常委李长春和中共中央政治局委员、中央书记处书记、中宣部部长刘云山，中共中央政治局委员、国务委员刘延东等来到刚刚投入使用的新闻出版总署新办公楼考察工作，并听取了新闻出版总署署长柳斌杰的工作汇报。<br />
<br />
&nbsp; &nbsp; 李长春充分肯定了新闻出版总署的工作。他说，近年来，特别是党的十七大以来，新闻出版总署坚持以邓小平理论、“三个代表”重要思想为指导，深入贯彻落实科学发展观，在改革、发展、管理等各方面做了大量卓有成效的工作。新闻出版工作表现出较强的政治意识、大局意识、责任意识，和党中央保持了高度一致。他还特别称赞了总署在加强行业管理方面所做的工作：既敢抓敢管，又改革创新，为社会、政治稳定作出了重要贡献。<br />
<br />
&nbsp; &nbsp; 而普通群众也会注意到，这几年，新闻出版总署、国家版权局、“扫黄打非”、著作权等这些词在媒体和社会上出现的频率多了，这说明新闻出版总署在全社会的影响力正在得到空前的提升。这是机构改革带来的好处，也是新闻出版人不负党和人民重托，兢兢业业、刻苦工作的结果。<br />
<br />
