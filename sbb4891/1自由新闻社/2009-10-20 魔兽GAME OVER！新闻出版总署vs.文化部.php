新闻出版总署终止《魔兽世界》审批<br />
本报讯(记者程晓龙)日前，新闻出版总署发出通知，终止《魔兽世界》(燃烧的远征)审批，退回关于引进出版《魔兽世界》的申请。通知要求，网之易公司立即停止违规行为，纠正错误，停止收费和新账号注册。新闻出版总署将视情依法对其作出相应的行政处罚，包括停止其互联网接入服务。<br />
<br />
　　新闻出版总署相关负责人告诉记者，在对网之易公司申请代理运营境外著作权人授权的网络游戏《魔兽世界》的审批过程中，新闻出版总署考虑到该游戏运营代理转换过程中有大量数据需要梳理，为保护玩家的切身利益不受损害，允许网之易公司在对不良内容进行修改的同时自7月30日起进行内部测试，重点是对玩家在游戏中的各种数据进行有效恢复，同时明确要求内测期间不得收费和提供新账号的注册。网之易公司对此表示完全接受和执行。<br />
<br />
　　但是网之易公司在未经新闻出版总署审批同意的情况下，于9月19日擅自收费并提供新账号注册的行为，已经造成事实上的公开运营服务，严重违反了国家关于网络游戏上网前须经新闻出版总署前置审批和境外著作权人授权的互联网游戏作品须经新闻出版总署审批的规定。为此，新闻出版总署发出通知，决定终止《魔兽世界》(燃烧的远征)审批，退回申请。<br />
<br />
<br />
文化部：《魔兽世界》运营正常合法 <a href="http://game.163.com/09/1102/19/5N507EMJ00313RAU.html" target="_blank">http://game.163.com/09/1102/19/5N507EMJ00313RAU.html</a><br />
网易游戏频道报道&nbsp;&nbsp;就新闻出版总署关于终止《魔兽世界》审批，网易游戏频道联系文化部文化市场司网络文化处处长刘强。刘强称，目前网络游戏相关审批工作属于文化部管辖范围，新闻出版总署仅负责新闻出版物相关审批，因此所谓终止《魔兽世界》审批的说法并不成立。同时，刘强还表示魔兽世界自7月30日以来属于正常运营，该游戏在经过代理权转交之后，并不存在内测和公测一说，目前《魔兽世界》收费运营以及注册新账户均属于合法行为。<br />
<br />
<br />
<br />
<br />
<br />
刚才听了几家公司老总的发言，很有感触，某些部门必须停止对网游企业的粗暴干涉。文化部的责任就是要保证中国文化产业，特别是游戏产业的长期发展。现在正是新旧交替，乍暖还寒的时候，我相信大家必将迎来阳光高照的日子，乌云必将随风而逝。<br />
<br />
　　很多企业反映，今年以来不断收到来自不同部门的各种通知，有些甚至还有矛盾，让企业十分为难。这个直接导致了9月7日中央编办发出对文化部、广电总局、新闻出版总署《“三定”规定》中有关动漫、网络游戏和文化市场综合执法的部分条文的解释的通知。《“三定”规定》中明确三个部门之间“划出”、“划入”的职责，请务必在年底前完成“划出”、“划入”工作，并将“划出”、“划入”情况向中央编办备案，同时进一步明确文化部是网络游戏的主管部门。最近，听说某些部门又发出这样那样的通知，这必将导致文化部加速全面接管。这是一个大是大非的问题，原则的问题，这是前天中央编办的同志讲的，在原则问题上我们绝不妥协。<br />
<br />
　　我们要通过不同的渠道来解决这些问题，况且有人还不让我们通过媒体发出声音。我相信这样的情况不会再持续多久了，大家要有一点耐心，我们都有很好的耐心，不要急，何去何从，我看大家应该以不变应万变。对于有些媒体刊登的内容大家一定要用自己的头脑来判断。<br />
<br />
　　这几年，我们对非法的游戏生产、经营活动进行过六批集中的整治，马上我们要开展第七批的整治，现在我们的整治不是口头谈一谈就可以了结的，是要依法行政，要受到行政处罚的。<br />
<br />
