													<h2>关于终止《魔兽世界》（燃烧的远征）审批的通知</h2>
						
						
																											<div id="postmessage_311032" class="t_msgfont">新出厅字〔2009〕473号<br />
　　北京市新闻出版局：<br />
　　《魔兽世界》是2005年经新闻出版总署审批同意、由上海九城公司引进并代理运营的境外著作权人授权的网络游戏。2009年6月，由于原授权协议到期，境外著作权人将《魔兽世界》代理运营权授予上海网之易网络科技发展有限公司（以下简称网之易公司），原审批文件自动失效。网之易公司需要按规定重新履行进口网络游戏审批手续。网之易公司于6月8日通过北京青鸟科教电子出版社有限公司向你局递交了引进出版的正式申请。<br />
　　你局审核后于6月18日向我署呈报了《关于北京青鸟科教电子出版社有限公司引进出版〈魔兽世界〉网络游戏的请示》。我署于6月22日正式受理并开始对该网络游戏进行审查，相关专家在审查中发现申报的《魔兽世界》存在许多国家法规明确禁载的内容。7月21日，我署考虑到运营代理转换中有大量数据需要梳理，涉及数以百万计玩家的切身利益。在按规定要求网之易公司与开发商――暴雪公司对不良内容进行修改的同时，允许网之易公司从7月30日起进行内部测试。测试仅限于原有注册玩家，重点是对玩家在游戏中的各种数据进行有效恢复。明确要求内测期间，运营公司不得收费，不得提供新账号的注册，采取一切必要措施，切实维护原有玩家的权益。网之易公司对上述决定完全接受和执行。<br />
　　 但是，9月19日，网之易公司无视国家的法规，在未经新闻出版总署正式批准前，擅自收费并提供新账号注册，造成事实上的公开运营服务，严重违反了国家关于网络游戏须经新闻出版总署前置审批和境外著作权人授权的互联网游戏作品须经新闻出版总署审批的规定。<br />
　　 根据这一违规事实，现研究决定：终止《魔兽世界》（燃烧的远征）审批，退回申请。网之易公司必须立即纠正错误，停止收费和新账号注册，并向公司所在地的上海市新闻出版局递交书面检查报告。否则，将依法对其作出正式的行政处罚，包括停止该公司接入服务。<br />
　　 特此通知。<br />
　　 新闻出版总署办公厅<br />
　　二九年十月二十九日</div>

							
							
							
							
