<img src="http://cppcc.people.com.cn/mediafile/201001/11/F201001110839402134488252.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
前两天本报记者前往刘翔在上海的千万元别墅，探寻他作为一个普通人如何度过那最艰难的养伤岁月。而在昨天，刘翔更以“邻家大男孩”的身份给记者拉起了家常，在轻松而充满人情味的聊天中，刘翔更首次谈到了自己小时候的梦想――他少时竟然和众多男孩一样，想当解放军，开大飞机！<br />
<br />
1、一心向往“擎天柱”<br />
<br />
　　“我觉得医生伟大，可以救死扶伤，很值得尊敬。但是我自己不想去做，开刀呀，打针呀，很恐怖。自己想当解放军，开大飞机。”刘翔的梦想和众多耍变形金刚长大的孩子一样，“好动顽皮，动不动就舞枪弄棒，精力好得跟马似的。”跟80后一样，小时候的刘翔同样为变形金刚着迷，上下楼梯都是三阶一步。刘翔爸爸回忆说：“刘翔二年级的时候转学，他看上去清秀文静，老师很喜欢。还不到一周，老师就来‘告状’了：‘从来没见过这么皮的孩子！’”就这样，好动顽皮的刘翔被选中去参加学校运动队，父母也赞成让刘翔在体育训练中得到一些纪律约束，就在这无心插柳中，刘翔迈出了运动生涯的第一步。<br />
<br />
　　“你们不知道，我还想过要成漫画当中的人物，比如变成阿童木、变形金刚什么的。我们80后受日本漫画影响比较大。”刘翔直到现在都对“擎天柱”心驰神往，几乎每次和姚明会面，双方交换的礼物必定是变形金刚。<br />
<br />
　　4、中国运动员一代比一代幸福<br />
<br />
　　大家都为刘翔奔波，刘翔在赛道上也在奔波，“也许每一个人实现心中的梦想，都需要很多很多的支持和帮助。一路走到今天，我能够梦想成真，真的离不开我的父母、我的教练的支持和帮助，就连食堂烧菜的师傅，也是一种对我的帮助，我现在甚至有些想念他。”刘翔承认自己是幸运而幸福的，“中国对运动员的机制是，解除所有运动员的后顾之忧。我们运动员肯定是很幸福的，一代比一代幸福。好的运动员都有赞助商。科技帮到了我，让我们更精确的知道自己的优点和缺点，哪些是需要改进的。”<br />
<br />
　　5、“跟着党走就可以了”<br />
<br />
　　对于未来，刘翔当然怀着巨大的憧憬。“运动员的生命是有限的，是吃青春饭的。就看青春过得值不值。每个运动员都面临这个问题。在这个环境里，比较单纯。若是退役的话，就将面临再选择，从事其他职业。感觉挺难的。”刘翔昨天说自己还没想过退役后去当一个教练。“我不需要想什么了，跟着党走就可以了。我没想过要去当一个教练，若是当一个教练就更加没有假期了，就照顾不到老人，妻子和孩子。教练会是一个成功的教练，但不一定是个成功的儿子、丈夫和爸爸。教练对我来说太崇高了，我觉得太难了。”</div>

							
							
							
							
