																											<div id="postmessage_429262" class="t_msgfont">刘翔，一个没长大的孩子<br />
<br />
<br />
刘翔最近在接受成都商报的采访的时候，说， <br />
“你们不知道，我还想过要成漫画当中的人物，比如变成阿童木、变形金刚什么的。我们80后受日本漫画影响比较大。” 刘翔直到现在都对“擎天柱”心驰神往，几乎每次和姚明会面，双方交换的礼物必定是变形金刚。<br />
<br />
阿童木，变形金刚对于这个阶段成长起来的男孩，自然是颇有共性的记忆，刘翔通过这些纯真可爱的话语，拉进了和公众之间的距离。<br />
<br />
不过，刘翔接下来说的话更可爱：<br />
“对于未来，刘翔当然怀着巨大的憧憬。“运动员的生命是有限的，是吃青春饭的。就看青春过得值不值。每个运动员都面临这个问题。在这个环境里，比较单 纯。若是退役的话，就将面临再选择，从事其他职业。感觉挺难的。”刘翔昨天说自己还没想过退役后去当一个教练。“我不需要想什么了，跟着党走就可以了。”<br />
<br />
“跟着党走就可以了”，这句话，对于这个教育制度下出来的人，相信也不陌生，在童年的某个阶段，几乎每一个人大概都说过类似的话语。“党是妈妈”，“党是母亲”，“党的恩情比海深” 这种意象浓重的情感比喻，曾经出现在几乎每一个小学生的作文里面。<br />
<br />
不过，童年终究会过去，小学生要变成中学生，中学生要变成大学生，，而刘翔也在2005年免试开始在华东师范大学读起博士了。因此，刘翔并不仅仅是一个跑步冠军，他还将获得一个博士学位。<br />
<br />
当我们把刘翔看作一个成年人，看作一个博士生的时候，再来试图理解他的“跟着党走就可以了”这句话，味道就显得有些怪怪了。出生于1983年的刘翔，其实已经27岁了，他早已经是成年人了。作为成年人，要进行自己的人生规划，要面对各种挑战，要理解社会运行的复杂机制，要承担一个公民的各种角色。而在现代社会中，党是一个现代政治机器，其中有复杂的利益表达，利益综合，政策执行，纪律约束等功能，这些功能的实现，需要其成员的积极参与。刘翔是中共党员，他有权利和义务参与实现党的这些政治功能。党并不是一个简单的路标系统，会指导刘翔跑步的方向，党也不是菩萨，能一直保佑刘翔。因此，说“跟着党走就可以了”，说明刘翔对自身角色和对党的功能都有严重的误解。<br />
<br />
我们可以理解刘翔，因为在他的成长过程中，他的大部分时间都在训练场和赛场度过，而在当下的竞技体育制度下，刘翔一直是被当作机器而不是一个独立自主的人培养的，因此面对记者，刘翔说出来的话，就像一个没长大的孩子说的话。<br />
<br />
作为对比，同样是80后的韩寒，其实只比刘翔大了一岁，但是10年前，韩寒就展示了他的独立不羁和对社会现象的独到理解，经过这些年丰富的社会阅历的积累，韩寒已经成长为一个著名的公众明星，他的公民表达甚至影响了社会进程。<br />
<br />
不是人人都能做韩寒，不过人人都需要成长。作为一个著名的公众明星，刘翔需要尽快走出精神上的孩童状态，走进这个没有擎天柱的，充满矛盾的，失去方向感的现实中国。</div>

							
							
							
							
