													<h2>争民主，游行不是只属民主党的</h2>
						
						
																											<div id="postmessage_713577" class="t_msgfont">AppleDaily苹果日报网上版－－2010-06-30<br />
--------------------------------------------------------------------------------<br />
争民主，游行不是只属民主党的<br />
<br />
今年七一，气氛凝重古怪。往年，民主理念的支持者，想都不用想，都会参加七一游行，坚持从维园走到政府总部，完成民主大道四公里，以示对民主的追求与执着。但今年七一，正如曾荫权所说，香港从此不一样了。我在电台「烽烟」，问听众会不会参加今年七一游行，不少民主派的铁杆都说：不去了。原因：心淡了、对争民主无望了。更多的人说：我不想与民主党同行。<br />
<br />
低估了对民运的伤害<br />
<br />
他们的答案是我始料不及的。民主党的八票赞成，通过二○一二政改方案，对民主运动的伤害和撕裂，民主党的领导人完全低估了，不是轻描淡写简单几句，说会继续争取终极普选，继续要求取消功能组别，就可以修补、就可以挽回失落的民心士气。<br />
<br />
一位八十后，在《城市论坛》质问民主党副主席刘慧卿，要求民主党避席不参加七一游行，以免挑衅群众。刘慧卿大声回应：「个游行你咩，你有乜S能量，乜S资格，唔畀人参加游行？」刘副主席说话的内容，相信无人能够辩驳，我也完全同意。刘慧卿承认，她的确变了，变得包容，懂得妥协，即使不同政见不同党派，都可以和衷合作，也会堆着笑脸的跑去与左派群众亲切握手，甚至成为维园阿伯的宠儿。但当她听到不同意见的挑战声音时，那个寸步不让，毫不妥协的刘慧卿又回来了，摆出一副巍然不动我是真理的态度。试问，这样做不是对四分五裂的民主运动，再踩一脚，永不翻身吗？<br />
<br />
不要低估「我不想与民主党同行」这种想法，因为不少伤透了心的民主支持者，确实认为民主党已不是他们的同路人，认为他们违反选举承诺，不愿与他们的暧昧立场走在一起。如果这想法成为今年七一游行的主流，那么游行人数肯定零星落索，可能创下○三年七一以来的最低纪录。<br />
<br />
果如是，政府建制保皇党就可以这样解读了：二○一二政改方案通过了，香港人可以有「一人两票」了，他们得到选举权的普及而平等，对民主心足了，对现状满意了，反对派搞的七一游行，他们拒绝再参加了。<br />
<br />
人数显示争民主决心<br />
<br />
这当然不是「我不想与民主党同行」的原意，但却止不了别人任意歪曲。在这里，我借用刘慧卿的同一种调子，大大声质问「我不想与民主党同行」的民主支持者：「个游行民主党咩，民主党有乜S能量，有乜S资格，令到你唔参加游行！」<br />
<br />
「一人两票」，绝对不是普及而平等选举，只有几百票甚至零票当选的功能组别，仍大剌剌地坐在立法会的殿堂上，仍然有分组点票的制度，强迫着多数服从少数；特区政府仍然向商界大地产商倾斜，官商勾结利益输送继续越演越烈；香港的政治制度，距离民主、距离终极普选仍然相当遥远。如果七一游行的人数大幅减少，只会示人以弱，如何能显示港人争取民主的决心？<br />
<br />
革命尚未成功，同志仍须努力！<br />
<br />
文：吴志森<br />
<br />
资深传媒工作者</div>

							
							
							
							
