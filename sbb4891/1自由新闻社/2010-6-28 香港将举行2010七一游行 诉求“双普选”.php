<br />
【明报专讯】「六二五」投票日最富悬疑张力的一场戏码并非源生于泛民山头，而是出自于建制阵营。那就是，陈鉴林的几颗欲滴未滴、将流未流的老人眼泪；那几闪泪光，若借用内地的时髦描述用语，便是「悬念」十足，可供延伸解读。<br />
