由于早前民主党支持政改方案，引发泛民激烈内讧，令人担心今年7.1游行人数大受影响，所以民阵召集人李伟仪昨日向市民作出最后呼吁，希望所有支持民主的市民今日上街，再向特区政府力争2012年双普选、废除功能组别，以及对连串人权和民生问题表达不满，千万不能示弱于中央，「如果今次政改方案通过就唔行出，等于向中央政府示弱」。<br />
<br />
「7.1是属于所有香港人」<br />
<br />
政改争议中曾互相抨击的泛民政党也放下歧见，携手呼吁支持者上街。公民党党魁余若薇承认，政改分歧引致泛民支持者争拗，但她认为不应因政见分歧，影响参加7.1游行，因为7.1不属于个人或政党，而是属于所有香港人，「每个市民都要参加7.1、因为7.1游行系属于大家、唔系某人、某党，大家都要出」。她不排除有人可能借泛民分歧，想在游行时搞事、抹黑民主运动，但她坚信市民是有分寸，不会做出任何过激行为。<br />
<br />
普选对手是一个专制政权<br />
<br />
近日备受政治盟友攻击的民主党主席何俊仁认为，市民坚持参加7.1游行十分重要，因为港人争取普选的道路上，对手是一个专制政权，「我O要寸土必争同打持久战，每个行动都会有效果，积累起上，有助提高抗争力度」。<br />
<br />
何俊仁称，7.1游行的重要性在于向中央表明港人至今未对政改「收货」，至今仍然坚持落实有普选路线图、废除功能组别的真普选，「最紧要系有意志、有信念，表现出持续抗争的决心」，他又不担心游行期间面对反对者批评，「我O疃嗄赵硕入面，困难的事都遇过好多，我唔觉今年系特别困难，我亦唔会怕有人对我O指摘，或者甚至有粗暴语言」。<br />
<br />
曾强烈批评民主党支持政改方案的社民连创党主席黄毓民，除了呼吁支持者今日上街，让7.1游行起着泛民团结的标志性作用外，他又要求支持者对民主党采取「三不」政策，即「不冲击」、「不嘈交」、「不反对民主党参与游行」。社民连也呼吁市民结束游行后不要留守政府总部，避免出现不必要冲突。<br />
