所以激动必有其它理由，而真实理由也不难看破，那就是叶刘淑仪所藐?嘴说的「有事锺无艳，无事夏迎春」。<br />
<br />
政改方案通过<br />
<br />
谁是输家？<br />
<br />
试问任何一位心智正常的人都怎可能不感到悲伤呢？建制议员在政治风向的突然转变下，自觉受到委屈了，明白进退失据了，面子保挂不住了，心里不服却又在权力压顶下不得不服，百感交集，五味交陈，或许在发言的刹那，脑海涌起一道声音，问自己，上面明明不准我们这样，现在却忽然答应他们那样，岂不视我们如只供任意摆来弄去的家具摆设？岂不全不替我们在港人面前留点颜面？万一方案真的通过，泛民坐大，我们被吞食了若干保护优势，往后的选战岂不更难打？又万一弄假成真，连这步都可以退让了，下一步真的来个取消功能组别，我们岂不更易由「执政党」变为「在野党」？<br />
<br />
千回百转，念念烦心，喉头由此哽住。一切跟民主无关，本来就跟民主无关，有关的只是面子荣辱，任何人都有底线，英雄人，奸雄有，庸才有，乜才物才都有，超越了底线便极伤感情，正如吴楚帆在粤语残片里常唱的两句歌词，「人非草木呀……谁孰无情……」，我们应该哀矜毋喜，只因终究同是港人。<br />
<br />
「六二五」之役结束了，政改方案被通过了，有人说民主党是最大输家，有人则说公社联盟才是，但依上所论，输得最惨的其实是建制阵营，因为他们在关乎特区民主前途的大辩论里，前言不对后语，左言难圆右论，并且语语支吾、论论吞吐，处境和表现皆尴尬万分。别说普罗泛民支持者看在眼里会笑在口里，即连建制自己友亦容易在心底产生不屑之情，即如一位建制议员所曾公开叹喟，「唉，我们都要向自己的选民交代？」。先别说在未来的选战里用「两票方案」取代「翻炒方案」变得稍为不利于建制阵营，仅就形而上的面子问题，建制派已经付出了严重的心理成本，即使多吃十次百次蛇宴或去多十趟百趟东莞摘荔枝亦难弥平创伤。<br />
<br />
大战过后，民主党急谋对策，一方面高调召开记者会宣布继续要求取消功能组别，另方面愿意于七一游行里排于队尾以示谦卑。其实，这还不够，民主党还可以做得更多。既然建制议员曾经哭?央求民主党投票支持方案以「合力推动香港民主」，民主党也答应了，那不妨顺水推舟，倒过来由何俊仁出马哭他一哭，眼泛泪光央求民建联加入七一游行，一起殿后走在队尾，用双脚来跟大家「合力推动香港民主」。邀请民建联，欢迎民建联，七一游行人数绝不嫌多；如果愿意加入，建制阵营才可真真正正的争回面子。<br />
<br />
马家辉 资深传媒人</div>

							
							
							
							
