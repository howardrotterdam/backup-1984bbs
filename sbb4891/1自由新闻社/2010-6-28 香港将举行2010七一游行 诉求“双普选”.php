--------------------------------------------------------------------------------<br />
暂放争拗　同心争普选<br />
<br />
【本报讯】今年7.1游行前，泛民主派阵营对政改方案的立场出现分歧，但支持不同路线的泛民团体暂且放下争拗，一同为7.1游行动员。其中，反对政改方案的职工盟表明，会动员成员参加7.1游行，支持政改方案的教协，也一如既往地动员会员上街。他们均认为，泛民必须团结，透过参加7.1游行，显示港人争取普选的决心。<br />
<br />
职工盟秘书长、立法会议员李卓人透露，泛民间的政改争拗，无碍动员会员7.1上街，除了一如既往以电邮及短讯动员，将另组织500名成员参加游行，要求中央及特区政府取消功能组别，以及将最低工资订于每小时33元。<br />
<br />
吁勿攻击民主党<br />
<br />
李卓人指出，虽然泛民阵营对政改有不同意见，但他呼吁社民连成员7.1游行期间保持克制，不要攻击民主党，泛民之间应该保持团结，才能向中央清晰表达普选诉求，否则令支持民主的民失望。教协副会长张文光也表示，他们已透过《教协报》呼吁会员参加今年7.1，他承认泛民争拗可能影响部份会员7.1上街的意欲。但他希望教协会员踊跃参加游行，因为7.1游行对所有支持民主的市民来说，是一次相当重要的活动，不论对争普选立场是否相同，也应该出来参加7.1游行。</div>

							
							
							
							
