泛民昨日呼吁各党派暂时放下争拗，7.1一齐上街争取双普选。图为08年4.7万市民游行场面。<br />
<br />
【本报讯】7.1游行前夕，香港大学最新调查显示，特首曾荫权早前落区「起锚」推销政改方案，不但无助改善民望，受访市民对他的评分反而下跌至48.5新低，连续四次调查低于50分水平，受访者对政府各方面施政的不满也继续升温。有泛民主派立法会议员认为，就算政改方案通过，有关终极普选及不少民生问题尚未解决，民间怨气高涨，所以市民7.1仍然要上街，反映对政府施政的强烈不满，令曾荫权不能逃避责任。<br />
<br />
记者：莫剑弦、许伟贤<br />
<br />
港大民意研究计划6月18至22日访问逾1,000名市民，调查结果显示，虽然曾荫权早前带领一众问责高官落区，希望打民意牌推销政改，但曾班子「起锚」后，民望继续下挫，市民对特首的评分再跌0.7分至48.5分，是连续四次低于50分水平，支持他出任特首比率再跌两个百分点，只有33%，反对比率则没有变化，仍然维持于51%，但也是连续三次超越50%水平。<br />
<br />
53%不满民生改善差<br />
<br />
除了民望毫无起色，市民对特区政府推动民主、改善民生的不满，也继续升温，其中对推动民主不满率上升两个百分点至45%、改善民生不满率也由上次的49%上升至53%，对维护人权自由表现不满率也大升7个百分点至41%，处理与中央政府关系满意率也下跌1个百分点到49%，不满意率大升6个百分点至28%。港大民意研究计划称，这些项目的不满比率全部是6至7年来的新高。<br />
<br />
唯一有改善的数据是市民对特区政府的表现，与上次调查比较，民对特区政府的整体满意程度显着上升，由5月的25%上升至31%，不满意率轻微上升1个百分点至44%。<br />
<br />
负责调查的民研计划总监锺庭耀指出，根据相关数据，香港的民生和民主问题仍然是特区政府需要更加着力改善的施政范畴。<br />
<br />
职工盟立法会议员李卓人认为，港大民调结果正好显示，就算曾荫权接纳民主党政改修订方案，令政改方案顺利通过，但实际上不论是支持还是反对政改方案的市民，仍不满意政府未有交出终极普选路线图，以及未有承诺取消功能组别；加上房屋、贫穷问题一直未有解决，民间怨气仍然高涨，曾荫权实在难辞其咎。<br />
<br />
勿让特首逃避责任<br />
<br />
李卓人呼吁，无论是支持还是反对政改方案的泛民支持者，今年7.1都仍然要上街，以表示仍然对政府施政相当不满。他说：「千万唔好俾曾荫权有借口，话政改方案通过，社会由乖蛊，所以市民唔上街，等谔颖茉鹑巍！<br />
