													<h2>苹论：绝不让政改一小步演化成最后一步</h2>
						
						
																											<div id="postmessage_713579" class="t_msgfont">AppleDaily苹果日报网上版－－2010-06-30<br />
--------------------------------------------------------------------------------<br />
苹论：绝不让政改一小步演化成最后一步<br />
<br />
1923年，鲁迅在北京女子师范学校发表演讲，指出女性要得到解放，最重要的就是要有经济权。他说：「世间有一种无赖精神，那要义就是韧性。听说拳匪乱后，天津的青皮（无赖）很跋扈，譬如给人搬一件行李，他就要两元，对他说这行李小，他说要两元，对他说道路近，他说要两元，对他说不要搬了，他说也仍然要两元。青皮固然不足为法，那韧性却大可以佩服。要求经济权也一样，有人说这事情太陈腐了，就答道要经济权；说是太卑鄙了，就答道要经济权；说是经济制度就要改变了，用不着再操心，也仍然答道要经济权。」<br />
<br />
明天是7.1，又到游行和争普选的时候了。今年7.1适逢在政改方案通过之后，泛民因政改而产生的争拗之声未平息，支持或反对政改方案的市民也有重大分歧。筹办7.1游行多年的李卓人表示今年最艰难，有市民为泛民争拗感到「无瘾」，对泛民尤其是民主党的表现「心淡」，以致觉得民主无望，说不会出来参加游行。<br />
<br />
这是一个关键时刻，比政改方案的通过与否更具关键性。它更需要市民的韧性坚持。鲁迅87年前的话，结合今天的形势，就是对民主，要继承这种「无赖精神」。你说普选已有时间表，不必争了，我们说要真普选，废功组；你说政改方案已走了民主一大步了，我们说只是一小步，我们要真普选，废功组；你说泛民已内斗得不可开交了，我们说那是泛民之间的事，作为市民，我们要真普选，废功组；你说泛民老大已妥协了，我们说要真普选，废功组；你说争普选的一些行动太激烈了，我们说没有激烈就没有改革，仍要真普选，废功组；你说这次政改已显示全由北京主导，香港人不必争了，我们说要真普选，废功组；你说香港人怎么争都斗不过中共这庞然大物，我们仍然说要真普选，废功组。<br />
<br />
支持民主普选的香港市民，有这种锲而不舍的韧性吗？当前真是一个考验。<br />
<br />
尽管笔者认同上周通过的改良方案，是向普选迈出的一步，但只是一小步，而且它隐藏着把这一小步当作是最后一步的阴谋。它会开启一种使功能组别议席永远存在的模式；它会毒化和腐蚀民主第一大党，使他们放弃抗争、寻求走入建制以享受权力滋味，甚至为能接近北京的最高权力而亢奋；它会使北京当权者认为，即使对普选另作解释，即使暗示不会废功组，只要给一点小小甜头，香港人也会喜孜孜接受；它会使北京堂而皇之撕毁《中英联合声明》和《基本法》关于「一国两制，高度自治」的承诺，公然站出来主导属香港内部事务的政改，而得到泛民第一大党和多数市民的背书。<br />
<br />
这是走向普选这一小步之后，香港人面对的艰难时世，我们认同改良方案，只因为我们的处境如马克思所说的「雅利安的驴子」，不是在两堆干草中选择哪一堆较好，而是在两阵棒打中选择哪一阵较轻。<br />
<br />
这一小步，绝不能让它演化成最后一步。泛民中尤其是民主党必须警惕，支持泛民的市民必须警惕，你们纵使一时之间「心淡」也绝不可放弃对民主的坚持。<br />
<br />
我们支持抗争，包括在法律许可范围内的激烈抗争。世界各地的民主，包括台湾的民主，都是一边在妥协中争取议会的普选议席、另一边不放弃街头激烈抗争而取得的。何况这种抗争有向内地人民示范和震慑中南海使他们作出让步的作用。<br />
<br />
我们也支持80后的民主苦行。他们可能已厌倦了游行，也厌倦了社民连的抗争方式，他们透过身体，以苦行形态去唤醒香港许多人已遗忘的「港人治港，高度自治」的应有权利，这是新一代、新形式的抗争行动。<br />
<br />
明天，请到维园，请走上街头，显示香港人争普选、废功组的坚强意志，即使面对目前这样令人困扰的艰难时刻，也绝不放弃。<br />
<br />
倘若你还没有想好，暂不想参与游行，也没有关系，只要你心中知道，绝不能让这政改的一小步成为最后一步，仍须持续争取，就可以了。<br />
<br />
让北京当权者和全世界都看到：香港有这样坚韧地要取得自己应有政治权利的市民，香港会有光明的未来。<br />
<br />
周一至周六刊出</div>

							
							
							
							
