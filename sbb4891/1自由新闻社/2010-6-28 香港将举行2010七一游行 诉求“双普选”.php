<br />
真的，泛民山头交出的政治戏码其实毫无曲折情节可言，反对的反对，支持的支持，立场势态皆早于投票前好几天已经酝酿成形，即连「表演方式」亦完全没有令人惊讶之处s张文光把一首徐志摩式的奶油新诗送给郑家富、梁国雄和黄毓民拍桌拍椅地羞辱同路人、何俊仁宛如粤语残片里的小媳妇般挤出委曲求全的可怜五官、吴霭仪娥眉怒蹙地痛骂有过诈骗案底的议员同僚、陈淑庄故作天真地以「小朋友」声调装幼扮嫩……统统不会带来意外的惊喜或怒气，因为这皆符合他们或她们的既定身分和形象，平日稍稍留心政情新闻的香港人无不心知肚明。反而向来硬朗木讷的陈鉴林突然在发言时鼻子一酸、喉头哽咽，开展一幅未亡人的悲情脸孔，彷佛压抑?满腔义愤，倒令人于觉得可笑之余稍感纳闷。咦，奇怪了，有戏了，不能不好好关注和讨论。<br />
