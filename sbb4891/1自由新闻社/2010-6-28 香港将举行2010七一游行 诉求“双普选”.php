<br />
此外，曾伙拍汇贤智库叶刘淑仪的医生史泰祖，今年会与同行蔡坚一起参加七一游行，他称是因为不忿民主党刘慧卿在港台节目《城市论坛》被辱骂，认为只要有诉求便可上街。至于加入汇贤后是否首次上街，史泰祖则以「忘记了」为由拒绝响应。</div>

							
							
							
							
