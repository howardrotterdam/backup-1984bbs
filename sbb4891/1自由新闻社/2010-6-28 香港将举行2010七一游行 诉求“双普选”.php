党派部署<br />
社民连拍片　避卷入冲突<br />
<br />
7.1游行期间，不少不满政改方案的人士，相信会以不同方式向支持方案的民主党抗议。民主党、职工盟及支联会将派出数十名纠察，为民主党游行队伍维持秩序；社民连也会派出10多名手持摄录机的成员，拍下现场冲突事件，以免被人栽赃嫁祸，又表明绝不希望游行出现任何意外。<br />
<br />
为免出现不愉快的冲突事件，民主党已向民阵表明行在游行队尾，并派出最少10多名纠察维持秩序。<br />
<br />
职工盟出纠察组人链<br />
<br />
职工盟立法会议员李卓人透露，职工盟及支联会今日也会派出30至40名纠察，在民主党游行队伍附近协助，避免民主党与其它游行人士发生不愉快事件。他认为会有市民借7.1游行直接向民主党表达不满，但他不相信有人冲击该党队伍。<br />
<br />
据了解，若届时有人冲击民主党队伍，纠察将组成人链，分隔示威人士及民主党成员，以免发生意外。<br />
<br />
社民连也公开呼吁支持者不要作出冲击行动，创党主席黄毓民昨日表示，为防别有用心人士混入游行制造混乱后，再嫁祸社民连，故此该党特别安排10多名持手提摄录机的成员，混入游行人群，一旦冲击发生时拍下情况，以免社民连再被屈，「我O呢次会好小心，真系唔想有人出事」。<br />
<br />
本报记者<br />
<br />
<br />
<br />
AppleDaily苹果日报网上版－－2010-07-01<br />
--------------------------------------------------------------------------------<br />
「维园行动」狙击民主党<br />
<br />
【本报讯】天要下雨，娘要嫁人，民主党难免成为今天游行的主角。「维园阿哥」任亮宪说，有「维园哥仔」已订制花牌「祭民主党」，也准备再次搬出「欺骗选民20年一铺变成民建联」、「坚定可信民主党关键时刻卖香港」、「刘慧卿大话精」等标语讽刺民主党，但不担心会引起冲突。<br />
<br />
任亮宪说，由于他要担任大会司仪，难参加今日的行动，但据他了解，「维园行动」成员除订制了讽刺民主党的鸭舌帽和T恤穿上身及即场发售外，沿途并向民主党游行队伍展示标语，以示对民主党放弃原则的不满，也可能会在游行开始前，在维园集合时「同O会一会」。<br />
<br />
促交代与北京谈判内容<br />
<br />
任亮宪认为，所谓「泛民分裂」，并非「狙击」民主党的行动造成，而是民主党放弃原则转变立场造成，且「泛民阵营唔系由政党组成，而系由选民组成」，泛民没有民主党也影响不大。<br />
<br />
「香港独立媒体」成员叶荫聪则在网上发起联署，要求民主党给市民一个完整交代，因为民主党立场转变跟与北京的谈判互动密切相关，公众对个中详情所知甚少，惹来「密室政治」的批评，民主党需更完整地公开交代细节。</div>

							
							
							
							
