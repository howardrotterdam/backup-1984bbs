																											<div id="postmessage_845727" class="t_msgfont">近日，不甘寂寞的李敖又登上《杨澜访谈录》，向韩寒“开炮”，称韩寒的书是“臭鸡蛋”。<br />
<br />
韩寒凭借其博客写作的超高人气，以及赛车手和作者双重身份，受到不少内地读者的青睐，并将他视为偶像。当杨澜希望李敖对韩寒进行评价时，李敖显得相当刻薄，他称：“韩寒就是长得蛮好看的，蛮体面的，人也很活泼，他知道媒体要什么东西，所以他给出来的就是媒体所要的肤浅的那一面。可是我们知道，人类社会是有很多深度的东西，需要很多时间累积的东西，如果否认了知识活动，那努力念书的人，去发明的人、去研究的人，这些努力都被抹煞了”。他还认为，韩寒是在投机取巧，是靠明星的光环让自己获得名利双收，社会不需要这么多韩寒这种人。<br />
<br />
李敖还表示，韩寒跟他儿子最大的不同，是他儿子根据档案的真实资料来写文章，而不是写一个人的感想，“韩寒的问题使我想起一个跟我同年的法国女作家莎岗，她就是写爱情小说的，她写一辈子我们也不会讲话。可是小说写到了人物疾苦的问题，就要看你的历练；谈到哲学要看你的深度。这些都没有，韩寒不会成功，因为他只是写感想。”<br />
<br />
<br />
　　尽管评价起韩寒头头是道，但李敖称自己并没有看过韩寒的作品，但他很肯定地说，他天生就有鉴别“臭鸡蛋”的本领，他只要不看的书，都是“臭鸡蛋”。 <br />
