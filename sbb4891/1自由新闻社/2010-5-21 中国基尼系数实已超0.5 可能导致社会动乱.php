													<h2>中国基尼系数实已超0.5 可能导致社会动乱</h2>
						
						
																											<div id="postmessage_628216" class="t_msgfont">新华社研究员：中国基尼系数实已超0.5 可能导致社会动乱<br />
2010年05月21日 06:57经济参考报<br />
新华社世界问题研究中心研究员 丛亚平 李长久<br />
<br />
改革开放30多年来，我国经济持续快速增长，到2008年，按汇率计算的我国国内生产总值(GDP)已跃居世界第三位。但是，由于长期积累形成的收入分配四大失衡，导致我国消费开支对GDP的贡献率一直呈下降趋势，而内需和消费不振又直接造成我国过分依赖外部市场，降低了我国的经济安全性和长远发展的潜力，甚至已经影响社会稳定。<br />
<br />
我国收入分配失衡的四大表现<br />
<br />
一、政府积累财富的比重越来越大而个人收入占比越来越小<br />
<br />
按发达国家的理念，政府如果花不了那么多钱就不该收那么多税，百姓把钱交给政府是为了让政府把钱花到为百姓服务上。然而我国各级政府竟然存下了大笔存款，成了食利者；同时地方政府还通过扮演“卖地商”的角色，聚集了大量卖地款，而且还属于缺乏监管的账外资金。央行公布的统计数据显示，“政府存款”项目下的资金额从1999年的1785亿元一路上升到2008年的16963.84亿元，猛增了9.5倍！20年来在劳动报酬和居民储蓄所占份额越来越萎缩的同时，政府储蓄率却在节节攀升，政府预算内财政收入占GDP的比重从10.95%升至20.57%，若加上预算外收入、政府土地出让收入以及中央和地方国企每年的未分配利润，政府的大预算收入几乎占到了国民收入的30%。<br />
<br />
因此，表面看起来中国的储蓄率很高，但其中真正属于百姓的储蓄占比并不高，据中国人民银行统计，2007年我国企业储蓄占国民收入的比重从10年前的12%上升到23%，而家庭储蓄所占比重却一直徘徊在20%左右。医疗及社会保障不到位，百姓的大量储蓄也不敢轻易花掉，从而导致居民消费能力受到严重抑制。<br />
<br />
二、财富越来越向少数人集中而工农大众收入偏低<br />
<br />
30多年来，我国企业特别是广大职工和基层科技人员为发展我国制造业、扩大对外贸易和开拓国内外市场都做出了很大贡献。然而令人遗憾的是，这些年来，财富却越来越多地向企业高管集中，而广大职工却没有相应提高收入和福利。从中国与发达国家小时工资水平比较看，中国大约是0.2美元左右 ，欧美国家大体上是25―30美元。这一方面反映了中国“劳动力成本低廉”的竞争优势，但另一方面，也存在劳动力价格低估和扭曲的问题。全国总工会2010年4月发布的一个调研显示，我国的国民收入分配格局中劳动者报酬占GDP的比重不断下降，而资本所有者和政府占比却大幅提高。从1997年到2007年，劳动者报酬占GDP的比重从53.4%下降到39.74%；企业盈余占GDP比重从21.23%上升到31.29%，而在发达国家，劳动者报酬占GDP的比重大多在50%以上。<br />
<br />
据国家统计局统计，职工平均工资最高的三个行业中，证券业平均17.21万元，是全国平均水平的6倍，其他金融业人均8.767万元，是全国平均水平的3.1倍，航空业人均7.58万元，是全国平均水平的2.6倍。而电力、电信、石油、金融、保险、水电气供应、烟草等国有行业的职工不足全国职工总数的8%，但工资和工资外收入总额却相当于全国职工工资总额的55%。<br />
<br />
近几年来，大企业特别是金融业领域高管与广大职工的收入差距明显扩大，特别是国企央企管理层自己给自己定的天价薪酬而造就的暴富者群体，已经引起国人的强烈不满。据统计，我国现有央企155家，央企管理层年薪动辄数十万、数百万甚至上千万，而大多数员工月工资一两千元，差距悬殊。新近发生职工罢工和流血事件的通化钢铁集团，据职工们反映，企业高管年薪上百万，而不少工人每月工资仅300元。如此巨大的收入差距，不仅严重脱离我国国情和基本收入分配原则，而且有违公平。<br />
<br />
世界银行报告显示，美国是5%的人口掌握了60%的财富，而中国则是1%的家庭掌握了全国41.4%的财富。中国的财富集中度甚至远远超过了美国，成为全球两极分化最严重的国家。<br />
<br />
三、城乡收入差距不断拉大而农民消费严重不足<br />
<br />
我国城乡人均收入差距之比已从改革开放初期的1 .8：1扩大到2007年的3.33：1。<br />
<br />
在农村，不仅社保、医保普及面小、福利低，而且长期在城市从事建设和服务业的农民工也是同工而得不到同等的社保和医保。截至2009年第二季度末，全国农村外出务工者达1 .51亿人，其中参加养老保险者占15 .88%，参加医疗保险者占27.5%，参加失业保险者占10.1%，参加工伤保险者也仅占33.5%。按三人之家计算，1.5亿农民工涉及到4.5亿人的负担。由于大多数农民收入低，加上缺少社会保障和医疗保险，消费能力被极度压抑。<br />
<br />
四、权力资本的暴利在扩大而中小企业普通民众的盈利空间被挤压<br />
<br />
由于我国政治体制改革滞后，对权力没有形成有效约束，腐败官员的比例逐年扩大，动则贪污受贿几百万甚至几千万的官员呈增长之势。不少官员或通过审批项目和提拔官员大举敛财，或在各类企业中拥有干股分红，让拥有官股保护的企业获得暴利的同时，却使通过正常渠道办事的普通守法企业步步艰难，使社会的整体运行效率大大降低。这种现象不仅会直接动摇我党根基，加大民众的离心力。<br />
<br />
前些年的国有企业贱卖使许多有权者获得了暴利，而大多数工人失业或收入下降。近几年的土地买卖，也在不断上演少数人暴富而大多数农民被严重剥夺的过程。许多农民在失去土地的同时失去今后的生活来源，增加了社会的不稳定因素。<br />
<br />
此外，股市也成了最大的扩大贫富差距的工具。通过不公平的发行制度，公众投资者比大小非等机构的持股成本高出十几倍，超高市盈率发行使股市在成批制造亿万富豪的同时，却使亿万股民辛苦积蓄一生的财产遭受重大损失。这样的制度形成对一部分人明显的利益输送，而对上亿股民、基民则构成财富掠夺，使大多数投资者的消费能力受到重创。<br />
<br />
另据社科院近日的一项调查显示，此次4万亿刺激计划 ，由于投资主要集中在重工业、基础设施行业，主要是资本密集型，创造的就业机会较少。由于更多资源流入强势企业和部门，对中小企业形成进一步的挤压。据统计，2009年上半年，占全国企业总数1%的国企获得全国借贷的91.2%，而民营企业仅获得8.2%。 <br />
<br />
贫富差距巨大会使社会矛盾愈加尖锐<br />
<br />
以上四大失衡导致我国贫富差距不断扩大，社会矛盾愈加尖锐。国际上通常认为，基尼系数0.4是警戒线，一旦基尼系数超过0.4，表明财富已过度集中于少数人，该国社会处于可能发生动乱的“危险”状态。我国基尼系数已从改革开放初的0.28上升到2007年的0.48，近两年不断上升，实际已超过了0.5，这是十分严重的信号。<br />
<br />
中国贫困人口数按照国际标准测算在世界上仍排名第二。我国政府2008年确定的贫困线仅为人均年收入785元，人均每天仅0.3美元，这与世界银行推荐的人均每天1.25美元的贫困线差距极为悬殊。<br />
<br />
