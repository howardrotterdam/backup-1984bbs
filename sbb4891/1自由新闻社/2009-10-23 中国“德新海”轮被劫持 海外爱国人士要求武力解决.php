													<h2>中国“德新海”轮被劫持 海外爱国人士要求武力解决</h2>
						
						
																											<div id="postmessage_297466" class="t_msgfont">潘宏斌：“德新海”号事件背后的中美较量<br />
<br />
作者：潘宏斌&nbsp; &nbsp;发布时间：2009-10-23&nbsp; &nbsp; 阅读次数：58次<br />
<br />
中国货轮“德新海”轮在印度洋被劫震惊了全国，也让世界一片哗然。这艘散装货船的船主是中国的，而且船体也在中国注册，是百分之一百地中国领土和主权。&nbsp;&nbsp;<br />
<br />
　　区区几个索马里海盗之敢于在1815公里外的公海劫持中国货船，听起来有点像天方夜谭：因为从上海到日本才不过840公里，还不及它的一半。可见索马里海盗对中国海军是多么的无视；简直就是对中国主权的侮辱和公开的挑战！&nbsp;&nbsp;<br />
<br />
　　一、“德新海”号背后的阴谋&nbsp;&nbsp;<br />
<br />
　　索马里海盗的背景不是一般人所想象的那样简单：索马里由于内战而处于分裂状态，1998年3个索马里的州宣布成立所谓的“邦特兰共和国”。这个“共和国”的官员最开始让渔民阻止在该国海域非法捕捞的外国渔船，后来他们惊讶地发现：竟然可以从这些外国渔船那里得到了“买路钱”！&nbsp;&nbsp;<br />
<br />
　　于是当地官员干脆将渔民组成“海岸警备队”开始猎捕更大的外国商船。随着劫持规模的扩大和赎金的不断增加，当地的官员将部分“收益”反馈给渔民，帮他们建立了别墅、旅馆和医院，甚至还帮他们购置快艇、枪支、GPS定位系统、卫星电话和导弹。他们还和在海上警卫方面享有盛誉的英国的HARTSECURITY公司签订了合同，来指导所谓的“海岸警备队”的成员参加快艇的操纵、尖端武器和微型电话的使用方法等专业训练。&nbsp;&nbsp;<br />
<br />
　　美国军方、媒体乃至美国商人与索马里海盗之间的关系已经不是什么秘密：美国《新闻周刊》在海盗那里常驻并享有专用特权；有美国情报机构背景的米切尔・巴拉琳以“美国商人”的名义充当海盗的“中间人”，全权代表索马里海盗收取赎金和参加谈判。&nbsp;&nbsp;<br />
<br />
　　“德新海”号是中国派舰队护航以来的第一艘大陆被劫货船，选择在这个时机和这个地点劫船无疑是具有深远的政治目的。&nbsp;&nbsp;<br />
<br />
　　其一，中国和俄罗斯刚刚在此海域完成联合军事演习，此举无疑给中俄两国来了一个下马威；其二，美国五角大楼刚刚发表了一份报告说：中国没有能力保护海外的商业基地和航道，此举很可能是对这一结论的战略试探；其三，中国刚刚向世界炫耀了实力---举行了60周年的阅兵大典，此举至少抵消了它的影响力，让中国丢脸。&nbsp;&nbsp;<br />
<br />
　　否则，你如何解释这地点竟然是如此地巧合：在远离海岸的980海里之外的印度洋劫持中国货船，而中国的三艘军舰也恰恰在1080海里之外的亚丁湾，就差那么一点点！&nbsp;&nbsp;<br />
<br />
　　二、中国国家安全意识应该与时俱进&nbsp;&nbsp;<br />
<br />
　　随着经济的发展，中国基本上成为海洋国家的一员，其视线就应当从沿海走向大洋。中国国家军事战略重心，也将由国土防卫过渡到中国在全球范围内的经贸权益的安全防卫。中国对“国家安全”的认识，也应该从传统的保卫本土安全，转变为保卫中国在世界范围内权益的安全。&nbsp;&nbsp;<br />
<br />
　　中国的外贸90%依靠海上运输。用中国交通部官员的话说：如果海上贸易受阻，那么将有“一半人挨饿，一半人受冻”。2008年欧洲成为中国最大出口市场，中国对欧洲的商品运输都需要经过亚丁湾这样的“危险水域”。维护海上通道安全已经成为中国新的重大战略利益。仅2008年1月至11月，航经索马里海域的仅仅在中国注册的中国船舶就达1265艘，就是运矿泉水价值也得超过上百亿元！&nbsp;&nbsp;<br />
<br />
　　中国国家实力的持续增长、北京奥运会和建国60周年大典无疑是向世界炫耀了实力，引起了美国和西方的恐慌。美国战略家们担心：在未来二十年内，中国将拥有挑战美国全球霸权的实力。所以美国一方面通过绑架中国的金融和经济，另一方面通过控制中国的海上通道以遏制中国的发展，试图将中国变成一个受美国单方面制衡和摆弄的木偶，就像日本一样。虽然中国的崛起和美国的衰退没有必然的联系，但美国不能不为此担心。正如美国学者保罗・布拉肯说，中国的强大使美国要保住超级大国地位的代价将会大幅度增加。&nbsp;&nbsp;<br />
<br />
　　与此同时，“友邦惊诧”论在中国也大行其道。为了不触动“友邦人士”那敏感的神经，拥有亚洲最大的驱逐舰、护卫舰、潜艇和两栖作战舰船部队的中国对出兵护航一直是犹犹豫豫，至今也没有派出一艘潜艇参与。面对中国商船在索马里海域遭受袭击几十次；大陆、香港和台湾的渔船、货轮屡遭劫持；船运的保险费和海盗的赎金不断地提高；国际社会的一再呼吁；中国民众的千呼万唤...中国不得已才派出三艘水面军舰，可见是多么的不情愿！&nbsp;&nbsp;<br />
<br />
　　受“韬光养晦”的影响，中国海军对海盗的“心慈手软”不仅没有感化这些化外刁民，反而滋长了海盗的气焰。海盗头子恩德布尔对美国记者就说：中国海军是一个“文明之师”。真不知道这是在表扬还是在讽刺。在亚丁湾上，中国军舰只对海盗进行警告和驱离，不动真格的。这就是为什么海盗不拿中国海军当回事，他们常常当着中国军舰的面继续攻击商船，甚至还敢用18艘小艇和中国海军玩起了“躲猫猫”的游戏...&nbsp;&nbsp;<br />
<br />
　　姑息养奸必然是后患无穷。“德新海”事件的发生既有中美大国之间的较力，也是中国国家战略观念的陈旧和落伍所致，绝非偶然！&nbsp;&nbsp;<br />
<br />
　　三、解救“德新海”号上的两种声音&nbsp;&nbsp;<br />
<br />
　　在如何解救“德新海”号上的问题上，一直存在着两种不同的声音。一种是不及任何代价对海盗进行坚决地打击；另一种是还是像以前那样，还是“以人为本”，支付赎金。前者认为国家的尊严高于一切；后者认为大国尊严不能以牺牲被劫持的人员生命为代价。&nbsp;&nbsp;<br />
<br />
　　这实际上还是“人权”和“主权”孰高孰低的老话题。船员和海盗的生命是“人权”，“德新海”号是中国的主权。中国传统中就有“民为贵，社稷次之，君为轻”的世界观，这与美国所宣扬的“人权高于主权”不谋而和。如果只从表面来看，它似乎是代表着普适价值观的永恒的真理。其实它是美国屡试不爽的“瓦解你们静悄悄”的一种思想武器。&nbsp;&nbsp;<br />
<br />
　　请问：美国为何不把此观念用于自己？！根据罗恩.马克斯韦尔《直面历史》中记载，为了维护国家主权和统一，南北战争中美国伤亡人数超过150万，是以后100年中美国所有对外战争伤亡人数的总和。南方诸大城市和庄园也被付之一炬，“战争带来的磨难超出我们的想象”。这就是所谓的人权高于主权？！&nbsp;&nbsp;<br />
<br />
　　再请问：科索沃被屠杀人数有2000余人，但是在卢旺达的种族灭绝中被屠杀人数达百万人。在卢旺达这个最需要人道主义干预的地方美国人为什么按兵不动，却跑到科索沃再次杀死了1000多人？难道这就是所谓的人权高于主权？！&nbsp;&nbsp;<br />
<br />
　　事实证明：所谓的人权和“人道主义”是美国用于干涉别国主权的一块遮羞布，它早晚会毫不羞耻地将同样的理念运用在中国的西藏、新疆问题上。试想：人权高于主权，如果台湾公投独立，我们要不要战争？如果战争，肯定会引发“人道灾难”，那么美国就会“理直气壮”地以人权高于主权干涉。&nbsp;&nbsp;<br />
<br />
　　所以，凡是直接或间接宣扬“人权高于主权”观念的人不是弱智，也是别有用心！&nbsp;&nbsp;<br />
<br />
　　中国在南海和藏南等国家主权问题上一再退让，很大因素是因为这条通往中东和欧洲的海上生命线的安全。归根结底，还是起因于中国的战略核武器的数量和质量不够。笔者十年前就呼吁中国应该放弃“有限核反击”理论，大力发展进攻性战略核武器。如果放弃不合时宜的“韬光养晦”的战略，放手发展六百枚以上的可以打到美国本土的洲际导弹和三十艘核潜艇及远程战略轰炸机和巡航导弹，这一切的麻烦就可以避免了。&nbsp;&nbsp;<br />
<br />
　　四、结论&nbsp;&nbsp;<br />
<br />
　　平心而论，索马里人原本善良淳朴并非天生的海盗。只因战乱所至、生活所逼使他们铤而走险、价值观发生扭曲。如果要从根上解决这一问题，国际社会必须正视这一地区的混乱问题，帮助索马里实现民族和解和统一。还必须对海盗给与严厉地打击，对所谓的“邦特兰共和国”的所作所为决不可一味地忍让和姑息。&nbsp;&nbsp;<br />
<br />
　　过去只要是中国商船被劫，中国总是痛痛快快地支付赎金，这已形成一种“国际惯例”。虽然这种“忍辱负重”能暂时应付目前的危机，但最后总会有你填不满的时候，绝非长久之计。对比之下，俄罗斯对海盗的穷追猛打、法国和美国对海盗的强行突击、印度海军的枪炮齐鸣对海盗更有震慑力。自从它们采取强硬态度后，海盗虽然嘴硬，声称要报复，但实际上尽量避免与上述国家的商船发生“冲突”。可见海盗也是“纸老虎”，欺软怕硬。&nbsp;&nbsp;<br />
<br />
　　国家的利益高于一切，国家的主权高于一切。为了维护中国的主权和尊严，我们强烈呼吁中国政府：出手吧！宁为玉碎不为瓦全，决不可再次向海盗低头服软！&nbsp;&nbsp;<br />
