																											<div id="postmessage_303540" class="t_msgfont">美国建国之初也曾被迫向北非海盗纳贡<br />
<br />
由于没有强大的海军，美国也被迫像欧洲国家一样，向北非海盗国家缴纳贡金。比如与的黎波里签订的“的黎波里条约”，就缴纳贡金100万美元， 此外， 还须送给阿尔及尔“一艘新造的 36 门炮的快速战舰， 每年还送一批价值 211 万美元的海军补给品”。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;<br />
<br />
本文摘自：《世界文化》2009年第5期&nbsp; &nbsp;作者：魏春洋<br />
<br />
美国刚刚建立的19世纪初，曾经与非洲海盗进行过十多年的你死我活的较量。<br />
<br />
在１６―１９世纪，这几百年时间，是世界海盗业发展的全盛时期。这些海盗中，尤以地中海上的北非海盗最为强悍。北非海盗以阿尔及尔、 突尼斯、 的黎波里等港与摩洛哥的一些港口为根据地进行活动。 当时，海盗的活动得到了当地政府的支持。在北非， 形成了4个海盗泛滥的地区――阿尔及尔、 的黎波里、 突尼斯、摩洛哥。北非海盗在地方首领“帕夏”的领导下，对来往于地中海的欧洲各国商船进行劫掠，欧洲各国经常为释放船只和海员为此要付出大量赎金。后来，缴纳赎买金制度变成了定期的贡金制度。凡是来往于地中海不缴贡金的商船，货物、船只经常被缴获，船员被卖作奴隶。严重威胁到了欧洲的贸易发展。是不是欧洲各国对北非海盗无能为力呢？实际上，像英国、法国等几个欧洲强国，特别是英国，拥有强大的海军实力，完全可以一劳永逸地消灭北非海盗，但他们出于自己国家商业利益的自私考虑，并不打算消灭北非海盗。因为强大北非海盗的存在，无形之中，帮助自己消灭了潜在的商业竞争对手，与其消灭他们，还不如缴纳贡金。英法等欧洲强国的卑鄙做法，无形之中给新生的美利坚合众国设置了重重障碍。<br />
<br />
18世纪末，在法国的帮助下，美国赢得了独立战争的胜利，建立了共和国。但新生的美国尽管政治上获得了独立，但军事上还十分弱小。特别是海军力量更为单薄。直到１８１２年，美国海军也仅拥有２２艘军舰。有人说，英国的军舰比美国的舰上火炮数目还多，就算美国的每门火炮都击沉一艘英舰，最后取得海战胜利的一方还是英国。在美国没有从英国独立之前，美国对外贸易的船只可以得到英国舰队的护航。但美国独立以后，情况就发生了变化。由于新生的美国没有强大的海军，它对外贸易的船只经常受到英国海军的挟制，更糟糕的是，美国商船还受到盘踞在非洲北部的地中海沿岸的海盗的严重威胁。由于没有强大的海军，美国也被迫像欧洲国家一样，向北非海盗国家缴纳贡金。比如与的黎波里签订的“的黎波里条约”，就缴纳贡金 100万美元， 此外， 还须送给阿尔及尔“一艘新造的 36 门炮的快速战舰， 每年还送一批价值 211 万美元的海军补给品”。美国的委曲求全，并没有换来北非海盗的怜悯，他们索求的贡金年年加码，到了１８００年，美国付给北非海盗的勒索金占联邦政府支出的２０％，已令美国政府不堪重负。<br />
<br />
第一次北非海战<br />
<br />
1801年，的黎波里的“帕夏”亚瑟夫认为美国送给他的贡金少于送给阿尔及尔的，因此要求美国缴纳２２５，０００美元的贡金。新当选的美国总统杰斐逊断然拒绝了这笔堂而皇之的勒索，的黎波里“帕夏”就命令砍倒美国驻的黎波里使馆的旗杆，以示宣战，随后其他三个北非地区摩洛哥、突尼斯、阿尔及尔的海盗也向美国宣战。杰斐逊这位文人总统表现出了军人的果断气概，下决心对的黎波里采取行动，他调动了海军最新最大的战舰７艘，后备支援船只二十余艘――这差不多是海军的全部家底――下令驶向地中海对海盗作战。<br />
<br />
这是一场实力悬殊的战争。北非海盗实力并不强大，美国军舰实力远在地中海海盗之上。北非海盗的船只适于掠劫，设计得窄长灵活，以利海上围猎。他们的战术与现在海盗战术相似。海盗船一旦咬上商船，立即迅速逼近，飞钩抛索，攀援而上，掳获人、货、船。因此海盗船着重配备密集的轻型近攻火力，登上甲板，依靠阿拉伯弯刀格斗迫使商船屈服。因此，在商船面前如狼如虎的海盗船，一旦遇上装有重炮的战舰，就无能为力了。虽然当时的炮弹只是个铁球，但是对于木质帆船来说却威力强大。所以，北非海盗和美国海军刚一交火，自知不是对手，就溜之大吉，躲在海港里，靠海岸大炮掩护，闭港不出。美国军舰在地中海南岸水域不断寻求战机，但多无所斩获。以致美国政府先后派遣了三支舰队对的黎波里进行封锁、攻击，但仍收效甚微。<br />
<br />
1803年，美国战舰费城号在巡逻中由于太靠近海岸而搁浅。海盗们靠海岸大炮的封锁，使得美国海军营救行动失败。船上３００多名官兵被俘，战舰摆在那里成了嘲笑美国海军的展品。海军上尉斯蒂芬率领一支敢死队，深夜偷袭的黎波里海港，纵火烧毁费城号。点火之前，还把船上３６门炮都摆到侧舷发射位置，炮口指向外面，让战舰像战场上的武士一样，高举着刀倒下去。烧毁费城号避免了海盗利用战舰和美国海军对抗，但这次偷袭行动的意义更重要的是精神方面，袭击的成功打击了海盗的气焰，鼓舞了美军士气，斯蒂芬成为海军最早的英雄人物。<br />
<br />
具有胜负决定性的行动是１８０５年４―５月的沙漠奔袭。指挥官威廉率领一支４８０人队伍，离开舰队，深入沙漠，穿插５００英里，奇袭海港德尔纳，令海盗首府的黎波里受到严重威胁。同时美国又利用外交手段，支持“帕夏”亚瑟夫的哥哥哈穆特复位，哈穆特本来是的黎波里君主的合法继承者，被弟弟亚瑟夫废黜其君位取而代之。美国和新“帕夏”哈穆特签署和平协议。根据协议，美国付六万美金赎回费城号的被俘官兵，的黎波里免去美国贡金。随后，地中海的突尼斯、摩洛哥、阿尔及尔也与美国签订了和平协议。结果5年的征战，美国终于得到了体面的和平协议。<br />
<br />
第二次北非海战<br />
<br />
可是，就在美国军舰起锚返航不久，美国的商船又遭到了北非海盗的劫掠。地中海三个地区突尼斯、摩洛哥、阿尔及尔海盗，并不遵守和平协议，继续在海上掠夺美国船只。多年下来，仅在阿尔及尔被绑架的欧洲人和美国人就有３万人，大部分被卖为奴隶，一部分人质被关押等待赎金。这些海盗还顺应国际局势的变化，在英美关系紧张时期，他们就加紧掠夺美国船只，勒索价码也狮子大开口。没有几年，北非海盗又卷土重来，严重威胁到美国对外商业贸易的安全。<br />
<br />
1815年3月，美国再次派遣海军出征地中海，由海军将领斯蒂芬和威廉指挥。这二位都是第一次北非海战的英雄，此时斯蒂芬已由上尉升为准将。<br />
<br />
斯蒂芬和威廉以其人之道还治之其人之身，大量扣押海盗战俘，作为人质交换在押美国公民。公民交换完了，就迫使海盗签署永久不再收取贡金的保证，最后，还要海盗象征性赔偿美国跨海出征的劳军费一万美元……美国气势咄咄逼人，征服了阿尔及尔，又转战突尼斯。到１８１５年６月３０日，北非海盗全部屈服，发誓再也不碰美国船只了。而且承诺将历年掠劫来的白人奴隶，一律恢复自由人身份。<br />
<br />
但到此时，北非海盗问题还没有彻底解决。北非海盗问题的彻底解决是在１８３０年之后。此时，原来的海盗受害国法国，利用自己的武力，先后将阿尔及尔和突尼斯占据作为殖民地，意大利则夺取的黎波里作为自己的殖民地。在欧洲殖民总督的铁腕管制之下，原来叱咤风云的北非海盗随之灰飞烟灭。<br />
<br />
人类社会发展到21世纪，北非海盗又因种种原因死灰复燃。美国在非洲又遇到了自己的老对手――海盗。但在今日，对海盗的日益猖獗国际社会已形成了一致的舆论，制止海盗行为，保持国际间经济活动的正常运行，是人们的一致诉求，相信海盗现象在各国协同动作下会得到遏制。 (本文来源：网易历史&nbsp;&nbsp;作者：魏春洋) xbchen<br />
