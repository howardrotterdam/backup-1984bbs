人保财险启动应急方案<br />
<br />
记者了解到， “德新海”轮由人保财险青岛市分公司国际部承保远洋船舶保险，保险金额近4000万美元，并按照惯例在国际再保险市场办理了充分分保。这艘货轮按照人保财险公司远洋船舶险条款承保了船舶一切险，该条款主要承保海上自然灾害与意外事故以及人为疏忽和部分责任风险。<br />
<br />
人保财险透露，2009年 10月19日，人保财险青岛市分公司接到青岛远洋公司电话通知，青岛远洋所属 “德新海”轮在印度洋向其报告，有武装海盗分子登船，随后失去联系。双方最后通话时间为北京时间19日16时35分。事发地点远离中国护航编队A汇合点南偏东1080海里。<br />
<br />
中国远洋集团旗下航运公司负责保险理财的一位负责人表示，集团旗下三百多艘船都买了两重保险，包括船舶险和船东责任险。该负责人指出，船舶险是保障船舶财产价值的，船东责任险则是对船员的安全进行保障。如果海盗劫持了船舶，所造成的损失应当由保险公司来承担。船公司通常按照船舶估值来进行一年的投保。<br />
