													<h2>金大中的政治生涯</h2>
						
						
																											<div id="postmessage_210491" class="t_msgfont">.<br />
<br />
韩国前总统金大中于本周二病逝。金大中的大半人生都在为韩国争取民主而斗争，后来又努力在朝鲜和韩国之间搭建桥梁。<br />
<br />
.<br />
<br />
<br />
<br />
<br />
.<br />
<br />
