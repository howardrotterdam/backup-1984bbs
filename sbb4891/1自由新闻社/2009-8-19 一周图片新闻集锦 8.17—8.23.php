<img src="http://chinese.wsj.com/pictures/photo/KimDaejung/02.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
Kim Chon-kil/Associated Press <br />
在金大中早年的政治生涯中，作为一名支持民主的反对党立法者，金大中与韩国的军事独裁进行斗争，赢得了人权和民主的坚定捍卫者的声誉。金大中在多起疑似刺杀行动中幸免，包括1973年戏剧性地被韩国间谍在东京一家饭店绑架。左图为金大中从绑架中获救返回首尔的家中后，与记者谈话。<br />
<br />
<br />
<img src="http://chinese.wsj.com/pictures/photo/KimDaejung/03.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
The Blue House Presidential Office/Associated Press <br />
1980年，金大中在南部城市光州因卷入一场叛乱策划被判死刑，该地是金大中的政治大本营。由于美国插手，判决被改为终身监禁，后又减刑为20年有期徒刑。后来金大中被流放到美国。左图为金大中在光州狱中读书。<br />
<br />
<br />
<img src="http://chinese.wsj.com/pictures/photo/KimDaejung/04.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
Choo Youn-kong/AFP/Getty Images <br />
金大中历尽浮沉，1987年成为韩国民主宪章3名主要起草者之一。这一年他参与总统竞选，以落选告终，5年后的大选中再次落败。左图为1993年12月18日金大中在投票后向记者致意。<br />
<br />
<br />
