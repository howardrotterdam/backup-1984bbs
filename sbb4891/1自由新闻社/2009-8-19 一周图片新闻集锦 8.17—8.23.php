Kin Cheung/Associated Press <br />
金大中因努力促成与朝鲜和解而获得诺贝尔和平奖。在因呼吸问题和肺部血块在韩国首尔医院Yonsei Severance Hospital接受了一个多月治疗后，于周二下午病逝。左图为2006年金大中结束采访之后的留影。<br />
<br />
