													<h2>谷歌并未作出妥协 中国官方“自找台阶”</h2>
						
						
																											<div id="postmessage_752021" class="t_msgfont">新闻报道 | 2010.07.20 德国之声<br />
<br />
工信部表示满意！谷歌做出妥协？<br />
<br />
据媒体报道，中国网络业务监管部门最近表示，谷歌承诺&quot;遵守中国法律&quot;,保证不提供&quot;非法内容&quot;,因此其网络运营商牌照年检通过。谷歌是否为了留在中国市场而做出了妥协？现在的谷歌究竟与从前有怎样的不同呢？<br />
<br />
7月20日，中国工信部通信发展司司长张峰在一次新闻发布会上表示，谷歌中国不存在违法内容，并接受主管部门的监督检查。经审核，其网络内容服务商（ICP）牌照年检整改事项基本符合规定。工信部的这番表态是否意味着，今年年初以来沸沸扬扬的&quot;谷歌退出中国事件&quot;暂时告一段落，这仍有待观察。但引人注意的是，几乎是在中国工信部就谷歌问题表态的同时，Google官方博客透露，今后在Google.cn的域名下将仅保留购物，音乐和翻译这三个产品，其他的搜索产品全部转移到Google.com.hk运营。<br />
<br />
谷歌并未作出妥协 中国官方&quot;自找台阶&quot;<br />
<br />
早在今年3月23日，Google在与中国官方就网络审查和黑客问题产生摩擦之后，就曾经把谷歌中国的搜索服务功能自动跳转至香港。当时而针对Google的这一做法，中国国务院新闻办曾经发表声明称&quot;外国公司在中国经营必须遵守中国法律。谷歌公司违背进入中国市场时作出的书面承诺，停止对搜索服务进行过滤，并就黑客攻击影射和指责中国，这是完全错误的。&quot;6月底，谷歌ICP牌照到期年检，工信部要求其进行整改。此后人们发现，谷歌中国的搜索业务不再自动转移到香港页面，而需要点击后，页面才会跳转。这是否是Google为了通过年检而做出的妥协呢？广州网络媒体人北风并不这样认为，<br />
<br />
&quot;在我看来，这不是一个妥协，而是更明确地告诉别人，Google.cn不能用了，只有用Google.com.hk才能继续使用google的服务。这对google并没有损失，反而对中国政府的形象可能有一些影响。起码让别人觉得，为什么Google.cn不能直接跳转到Google.com.hk的域名。如果中国互联网是一个宽松的环境的话，这种跳转绝对是可以被许可的。&quot;<br />
<br />
按照北风的说法，谷歌中国搜索服务目前的状态其实与&quot;整改&quot;之前并没有什么太大的区别，只是从&quot;自动跳转&quot;到&quot;点击跳转&quot;,需要用户多按一下鼠标而已。那么，这一下鼠标点击动作对于工信部而言就真的那么重要吗？独立作家冉云飞表示：&quot;其实是官方掩耳盗铃，中国政府是世界上最好面子的政府。据我所知，谷歌的搜索服务并没有回（到中国大陆）来，所以中国官方（宣称满意）是自己给自己下台阶。&quot;<br />
<br />
没有&quot;翻墙&quot;工具 Google.com.hk的搜索结果更受制约<br />
<br />
转移到香港之后，谷歌中文搜索将不再进行自我审查，但是中国大陆用户所得到的讯息仍然经过了被称为&quot;GFW&quot;的官方审查系统的过滤。那么与以前自我审查的谷歌相比，现在的谷歌搜索是不是能够提供比从前更多的自由资讯呢？北风认为，事实可能恰恰相反，&quot;对于熟悉'翻墙'工具的人而言，使用Google.com.hk可以得到全部的搜索结果。而使用原来的Google.cn的话，即使用'翻墙'工具也只能得到片面的搜索结果。但如果都不使用'翻墙'工具的话，那Google.com.hk搜索结果受到的制约可能更大一些。因为即使一些看起来与敏感问题无关的'关键词',例如'胡萝卜',也会被中断链接，搜索不到结果，而在以前的话你还是能得到部分结果 .&quot;<br />
<br />
然而，对于大部分中国网络用户而言，&quot;翻墙&quot;毕竟还是一个高难度的技术动作。更不用说，绝大多数人可能根本就没有意识到自己所检索的信息已经遭到了审查和过滤。因此，对于类似北风、冉云飞这样的&quot;翻墙老运动员&quot;而言，不管官方与谷歌之间&quot;拔河&quot;的最终结果如何，他们总是有办法找到未经审查的网络资讯，而对于大多数人而言，却依然身处网络审查中而不自知。独立作家冉云飞认为，这是一个现实情况，但是&quot;我并不觉得悲哀。这种现象只是暂时的事情。要知道中国互联网真正大规模兴起也才12年。这12年里，中国民众的信息获得渠道和量，完全是49年之后的几十年所无法比拟的。由于信息获得渠道的增加，民众对各方面的看法在改变，我对此还是有信心的。&quot;<br />
