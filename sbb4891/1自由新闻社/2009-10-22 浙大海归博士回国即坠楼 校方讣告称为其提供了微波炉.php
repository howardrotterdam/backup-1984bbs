主持面试的正是岩土所所长陈云敏。他随后把涂博士带到学校人事处。分管人事处人才引进办公室的一位副处长朱晓芸，正是在此时第一次见到涂博士，她向他介绍了学校引进人才的相关政策。按照惯例，她知道，只有学院看中的人，才会带到人事处来谈引进的细节，所以她觉得学院对涂的才干应该是重视的。<br />
<br />
她后来和涂博士又进行了几次E-mail交流，涂博士是她工作和服务的对象之一。浙大正在加快步伐建设“世界一流大学”，每年从海外引进的人才，以半百计。<br />
<br />
3月结束在杭州的交谈后，涂博士回到芝加哥市。一个月后，浙大的录用通知发到了涂博士手上。他在接到这个新进人员聘任通知书后，通过电子邮件回复“愿意接受你们提供的岗位和待遇”。<br />
<br />
浙大的电子邮件中，确认给涂博士提供教职，但并未明确具体的职称。<br />
<br />
6月初，涂博士提前结束了美国西北大学的博士后工作回国。本应于6月底毕业的他，向博士后老板申请了提前出站。他告诉妻子何晶(化名)，国内有项目等着他。<br />
<br />
何晶也是留美博士。和涂博士清华大学年级第一的成绩相比，何晶在国内的学习记录毫不逊色。她本科在北方某名校读经济相关专业，毕业后追随丈夫到了美国。她也拿到了全额奖学金并取得了博士学位。这是她第二次跟随丈夫转换自己的生活环境。<br />
<br />
涂博士选择归国并且定居杭州，是非常自然的事情。了解涂的人说，他是一个孝子，姐姐一家在杭州生活，父母在金华，回到杭州于是成为首选。<br />
<br />
6月12日，涂博士与浙大签署了聘用合同。何晶也向浙大投了一份简历。<br />
<br />
6月是令人愉快的季节，西湖的荷花开满池塘，这也是杭州最美的季节，一切似乎都在向好的方向发展。<br />
<br />
他们的女儿，本来在金华老家由何晶父母抚养，可以预见的是，一家三口或许能在杭州团聚。<br />
<br />
人才战略<br />
<br />
涂博士属浙大“1311计划”引进的1000名学术骨干之一，每月到手的收入2000多元。<br />
<br />
涂博士的回国，可以看做浙江大学“1311工程”的一部分。<br />
<br />
浙大党委宣传部部长沈文华的办公室，一份人才战略年计划的文件摆在案头上。<br />
<br />
今年是人才战略年，几天前，沈文华告诉本报记者，浙江大学启动了“1311计划”，即建设一个结构分明的创新团队，这个团队的组成是，100位大师，300位核心人才，100个创新团队，1000个学术骨干。<br />
<br />
他补充说，大师这个称谓有点不准确，但至少应该是长江学者、特聘教授级别。核心人才属于在某个方面已经具备了一定造诣的教授，而涂博士应该属于1000个学术骨干之一。浙江大学教授上千，但并不是所有教授都能够进入骨干队伍。换句话说，他感觉建工学院对涂博士的学术水平是很看重的。<br />
<br />
涂博士归国有其时代背景。上个世纪末，公派被奖学金取代，最拔尖的一批学生手持GRE红宝书走出国门，美国、英国、德国，依次成为留学生的首选。涂博士就读的西北大学，正是美国名校之一，位于靠近芝加哥市的密歇根湖畔，在芝加哥市中心亦有分校区。<br />
<br />
出国潮之后，最近几年，中国的海归潮初具规模。教育部统计的数据表明，2004年，海归人数超过两万人，2006年，超过四万留学生回到中国内地，2008年，在全球金融危机的背景下，这个数字突破5万。<br />
<br />
在高校或者科研所任职，是海归大潮的一条重要支流。十年前，还不乏毕业于美国名校的海归博士回到中国某所知名学府、一进校就是教授的先例，而现在，行情有所缩水。<br />
<br />
学校要主动走出海外筑巢引凤的，主要针对在海外拿到教职的资深学者。比如位于浙大紫金校区的教师宿舍区里，还预留着近百套没有出售和分配的住宅。不过，浙大说，那些都是给“大师”们准备的。<br />
<br />
年轻人的成长需要一个过程，浙大党委宣传部部长沈文华评价说，并不是一进校就能担当重任。资历越深，掌握的资源自然才能越多。<br />
<br />
虽然涂博士在和芝加哥朋友的交流中，对困难早有预见。然而，困难似乎比他想象中大得多。<br />
<br />
涂博士并未如自己事先预料的那样，回国就有科研项目可做。6月，他回到老家浙江省金华市休息了一段时间。<br />
<br />
7月，涂博士回到杭州。28日，他租住在由浙大提供的位于玉泉校区求是村的教师公寓。学院提供的信息表明，涂博士居住的公寓建筑面积57平方米。在涂博士的讣告里也提到了这个公寓，说明“内含独立厨房、卫生间以及电视、冰箱、空调、微波炉、电磁炉、床铺、桌椅等家具和电器设施”。<br />
<br />
这正是涂博士居住了两个月，而后纵身跳下的综合楼，它位于浙大玉泉校区正门的马路侧对面，是一栋上世纪90年代末的建筑。从简陋但还算干净的楼道上，可以一眼望到老校区，郁郁葱葱的树木成荫，朝气蓬勃的学生来来往往，校园内挂着学术交流会议的横幅。<br />
<br />
综合楼的每层都有一条长长的走廊，并排着的是给年轻教师做过渡安置用的住房。建筑面积 50-60平方米，套内面积在40平方米左右，就像三个排放着的火柴盒，从外到内分别是厨房，客厅，卧室。浙大人事处工作人员告诉记者，综合楼住房月租 2000元左右，略低于市场价，从工资里扣除。他们说，涂博士曾经在别的地方住过，对比后，还是搬到了这里。<br />
<br />
涂博士生前住过的屋子，大门的纱窗蒙满灰尘，显示已经空置多时。隔壁却传来新鲜肥皂的香味，一间屋子门口放了很多小花盆，花香缭绕，是人间烟火的味道。涂博士纵身一跳，却把自己和人间烟火的味道隔绝了。<br />
<br />
浙江大学分管人事的朱晓芸副处长告诉记者，涂博士的待遇按讲师发放。并且给他提供4万元的房贴，支持他在杭州购房、安家。这是浙大专门针对引进人才提供的特殊待遇。除此之外，涂博士还享受学校给所有教职工提供的待遇，包括在进入浙大的10年内，拿到每个月1500元的房贴，如果涂博士将来评上教授，这个数字会翻倍。收入里还包括每个月1000元的年轻教师津贴、国家规定工资、学院补贴。每个月的收入，应该足够他在学校安心教书治学。<br />
<br />
然而，这也许只是一个理想状态，涂博士的女儿不久就要上幼儿园了，现在国内大城市的幼儿园收费不菲，比小学还贵。涂博士的妻子何晶也不顺利，她没能在杭州留下，只好一边住在老家带孩子，一边找工作。实际情况是，涂博士有一个家庭需要支撑。<br />
<br />
夫妻二人虽然都是留学生，在美国有全额奖学金，但有小孩子要养，仍然不能在异国他乡存下一笔数目可观的钱，足以让他们在刚归国时作为缓冲。<br />
<br />
而在号称人间天堂的杭州，房价毫不逊色于上海。浙江大学紫金新校区虽然位于并不那么被看好的西区板块，每平方米价格仍然要直逼2万元。<br />
<br />
记者咨询了建工学院和涂博士同样职称的一位老师，他的待遇也是按照讲师发放，在扣除房租、所得税之后，每个月到手的有2000多元钱。这位老师和涂博士年龄相仿，国内某知名高校博士生毕业。以这位老师的收入计算，他若要贷款买下紫金校区附近一套 90平米左右的住宅，以三成首付计算，独立支付要不吃不喝将近20年。他没有掩饰失落的情绪。他的同龄人若早早参加工作，已赶在中国房产价格狂飙突进的 21世纪初期，安居乐业。<br />
<br />
意外的抑郁<br />
<br />
在亲友、同行眼中，涂博士自信开朗，但自杀前一个月，他要服用药物帮助睡眠。<br />
<br />
无从考证涂博士是否也有相似的心理落差。据他的亲属转述，他告诉家人，工资是按副教授资格发放。<br />
<br />
可以确定的是，涂博士出事前，他的工作仍在按部就班进行，他相继参加了学院组织的留学归国人员座谈会、环境岩土工程国际学术研讨会筹备及会务工作，并担任了2009级本科新生班主任。<br />
<br />
涂博士的悲剧发生后，学校去学生宿舍作过调查，了解到，从开学到他出事不到一个月时间，他一共去了5-6次学生宿舍，与学生交流，耐心解答疑问，为人师表。<br />
<br />
然而，不为人知的是，也是在8月，他开始焦虑，并开始服用药物帮助睡眠。<br />
<br />
无从得知，涂博士的焦虑从何而来。从他搬到求是村综合楼，他大部分时间独自居住。<br />
<br />
在他的妻子和至亲眼里，涂博士一直是一个很自信的人。<br />
<br />
这和他的朋友同学的印象相符。在芝加哥朋友眼中，涂博士对人热情宽厚开朗，对学术潜心追求。朋友回忆，涂博士还是湖边烧烤的积极组织者，给友人带去家乡的茶叶。<br />
<br />
高中同学在回忆时，也无法把他和焦虑联系起来，他是一个喜欢挑战自我的人，在没有丁俊晖的年代里，只有小混混才打桌球，而涂博士的桌球技术是那一帮同学里最好的。<br />
<br />
涂博士也不是只知道死读书的书呆子。作为一个理科生，他还有自己人文方面的理解。清华校友录上，记录着涂序新和同学之间的辩论，在涂博士的发言中看得出，他虽然身在国外，但是对中国社会现状、法律问题很关注。他还讨论了知识分子在中国社会发展中应该起到的作用。<br />
<br />
可以确定，即使焦虑过，涂博士在8月还没有绝望。学校提供的记录表明：<br />
<br />
9月1日到8日，涂博士参加了浙大的新教师岗位集中培训。<br />
<br />
9月8日，浙大启动了下半年专业技术职务评审工作。<br />
<br />
9月11日，涂博士在学校“专业技术职务聘任系统”中填写了申报副教授的相关信息。<br />
<br />
至少，他对生活中的某些事情还怀抱希望。<br />
<br />
然而形势急转直下。6天后，也就是9月17日凌晨2时，涂博士从11层楼顶跳下来。他写好了6页遗书，在遗书中，他向妻女、父母、姐姐表达了歉意。<br />
<br />
就在前一天晚上，何晶还和丈夫通了个电话，约定第二天一早去看他。<br />
<br />
涂博士是一个喜欢在学术圈发展的人，之前有他为了博士后研究职位，放弃美国得州工作职位的记录。在西北大学的个人简介中，他留下了这样一段自我描述，“在Finno教授指导下完成博士论文后，我继续跟随Andrade教授做博士后研究工作。我的研究重点是开发岩土建模和新的计算工具。我喜欢西北大学，在那里我能够与其他不同背景的研究人员在许多跨学科项目领域合作。”<br />
<br />
涂博士的学术潜力已经部分得到了证明。用Googlescholar(谷歌学术搜索)可以搜到他发表的6篇学术论文，其中3篇是SCI核心期刊。作为第一作者发表的被引用次数最多的文章是2008年发表在《国际工程数值期刊》的一篇文章《微粒的标准计算静力学平衡》。<br />
<br />
涂的研究，集中在岩土工程的计算方法和模型领域。在国内大搞工程基建的背景下，应有广泛的应用前景。以杭州本市为例，去年年底，轰动全国的地铁塌方，正好是由于在偏软的地基中施工不当导致。<br />
<br />
浙江大学的土木工程专业在全国名列前几位，其中岩土工程学科为国家重点学科。岩土所所长陈云敏教授毕业于浙江大学，也是国内岩土界的实力人物。他评价说，涂博士在科研领域做出的贡献是优秀的，这也是学院看重并从西北大学引进他的原因。对于涂的离开，陈表达了惋惜之情。<br />
<br />
职称与坠楼<br />
<br />
网络盛传浙大曾给涂博士口头承诺，但却未能履行，学校认为指责不合常理。<br />
<br />
从9月11日填报申报副教授信息到9月17日跳楼，涂博士到底经历了什么？<br />
<br />
他的死，最初是由分管浙江大学玉泉校区治安工作的派出所介入调查的。记者咨询了杭州公安局西湖区分局政治处，一位负责人告知，两天后，派出所询问了他的朋友、亲人、邻居、学校，警方排除了他杀嫌疑。<br />
<br />
9月底，建工学院的网站上挂出了涂博士的讣告，解释了涂博士的死因―――“因病”“坠楼”。浙大说，这是最后的调查结论，目前善后工作已经接近尾声。但学校没有具体解释“因病”和“坠楼”为何会联系在一起，他们说，这涉及涂博士的个人隐私。<br />
<br />
涂博士死后，各种猜测仍然在网络上发酵。有归因于学校，有归因于家庭，有归因于博士的心理素质。人们站在各自不同的角度揣摩、猜测。网络盛传浙大曾经给涂博士口头承诺，但却未能履行。<br />
<br />
对这种指责，浙大觉得不符合常理。<br />
<br />
建工学院党政办公室科长金卫勇告诉本报记者，涂博士出事后，他们专门查看了学院和他来往的E-mail，都是“教职”字样，没有出现“副教授”。<br />
<br />
人事处副处长朱晓芸向记者介绍，“教职”的意思是，涂博士在海外有两年的博士后研究经历，按惯例一进校就至少是讲师。至于能否拿到副教授职称，则要看9月的评定。浙江大学一年有两次高级职称评议，第一次是5月的特评，专门针对海外归国人员，第二次是在9月，针对全校教职员工。<br />
<br />
但不知何故，涂博士没有报名参加5月的特评，一些信息表明，他认为参加9月的评定更有把握。<br />
<br />
而涂博士“因病坠楼”正好发生在9月职称评定期间，距离他填写申报信息不到6天。舆论普遍猜测，他的死是否和提前知道了评选结果有关，是否有人提前找他谈过话。<br />
<br />
学校有关方面否认了两者之间可能存在联系。建工学院说，9月上旬只是报名，工学部还没有公布文件，确认评选标准。<br />
<br />
工学部是由土木、材料、环境等工科院系的资深专家组成，是相关院系高级职称评定的责任机构。岩土所所长陈云敏正好在这个机构担任副主任。他也否认有人能左右评选结果，他说，职称评定是民主评议，不是谁能说了算。<br />
<br />
至于是否有人在职称评定前后找涂博士谈过话，已无法考证。人事处说，参评应该是涂博士的权利，谁能找他谈话让他不要参评？令他们感到惋惜的是，按照涂博士的条件，评副高职称并非没有把握，或许按正常的程序他是能评上的。<br />
<br />
9月中旬，涂博士诊断出抑郁症，遗书里，他没有责怪任何人，他说，我只想安静地离开。<br />
<br />
不知何故，他留下了这样一段话：“在此时刻，我认为当初的决定下得是草率的，事后的发展完全没有预计，感谢一些朋友事前的忠告。国内学术圈的现实：残酷、无信、无情。虽然因我的自以为是而忽视。”<br />
<br />
高危群体<br />
<br />
他们从小就承担了太多社会期望，而以世俗观念看，他们中的很多人并未立刻接近成功。<br />
<br />
从归国到坠楼，不过3个月，涂博士就从逐渐焦虑到抑郁。他的死已经由浙大上报教育部。<br />
<br />
9月29日，涂的追悼会在杭州殡仪馆举行。他是金华人，他的朋友、亲戚、同学从金华赶来，把大厅挤得满满的。他所在的建工学院部分领导、师生也参加了追悼会。<br />
<br />
追悼会上，当着所有亲朋好友和同事的面，何晶泣不成声。追悼会的第二天，是女儿三岁生日。何晶希望，亲友在女儿懂事之前，帮她为孩子编一个美丽的谎言。耐人寻味的是，在她断断续续不多的言语中，她特别强调了涂序新发表的3篇SCI核心期刊文章，他的成绩是任何人都不能抹去的。<br />
<br />
涂博士从小到大都是家庭的骄傲，家族弟妹学习的榜样。失去这个儿子、弟弟，他的父母、姐姐痛不欲生。他们不敢相信他已经离开。他的姐姐现在还无法正常上班。妻子也沉浸在悲痛中，和女儿度过了一个最难过的中秋节。<br />
<br />
同在建工学院的一位年轻同事为他感到异常惋惜。这位年轻老师也同样是拿着低薄的薪水，年近而立，从小到大背负了太多希望，却仍然无法回报父母。<br />
<br />
浙大党委宣传部部长沈文华说，涂博士刚回国，适应环境有一个过程。学校让他当新生班主任，还让他参加一些学术会议的筹备工作，正是帮助他适应。但从事后看来，对他的关心还是稍微少了一点，没有注意到他的状态。他说，如果本校毕业，还有一个现成的朋友圈子可以倾诉。涂博士事件后，对青年教师会有更多关心辅导，特别是对从外校来的年轻教师和年轻海归人员。<br />
<br />
涂博士所在的建工学院开始找年轻教师谈话，让他们有什么困难要和学院说。<br />
<br />
年轻海归并非人们通常想象中的弱势群体。他们有知识有文化，也有前途。然而事实是，他们也是一个高危群体。他们从小就承担了太多社会期望，而以当代中国社会的世俗观念看，他们中的很多人也许并没有立刻接近成功。<br />
<br />
S现在香港某所高校当博士后，她和涂博士同一年出生。她说，他们这一代人从小是好学生，却错过了中国社会的高速发展。她特别能理解涂和他的妻子。<br />
<br />
追悼会第二天，正好是涂博士女儿的三岁生日，孩子还远未到懂事的年龄。鲜花丛中的父亲形容瘦削，无法吸引她的注意。她似乎认为，父亲只是睡着了，还会再醒。<br />
<br />
但她的父亲涂博士永远不会醒了，父女有一张合影，孩子坐在父亲肩膀上，脸上写满笑，涂博士两手紧紧抓着孩子的脚，笑盈盈地望着镜头。这样的合影今后不会再有。涂博士只能在照片上看着女儿慢慢长大。<br />
<br />
老同学追忆<br />
<br />
“他是我们中的牛人”<br />
<br />
“土匪”是他初中时的外号，我曾经听他说，到了大学，他升级叫做“屠夫”了。一个身高和相貌都是一等一的帅哥摊上这么个名字还能沾沾自喜，应该知道他是一个乐观通达的人。随便说一下，我的绰号叫“无赖”，但我却是小鸡肚肠耿耿于怀，谁这么叫我都会咬牙切齿一番的。<br />
<br />
他是一个刻苦学习的人，但不是一个死读书的人，这点毫无疑问。如果说抛开他硕大的耳朵和手脚外，留给我印象最深的是那一手漂亮的毛笔字，他的毛笔字是自小一直到大学都没有停止（练习）。还有就是球打得好。我说的球是台球。在我们那个年代，那个没有丁俊晖和傅家俊的时代，打台球是街头混混才干的事情。我永远记得在海淀图书城附近，一干同学打台球被“土匪”涮得一塌糊涂的场景，他那谦虚的得色至今我还能回想起来。要说玩，他是一个会玩的人。简单地说，他是我们那批人中的牛人。要说吃，他也能吃，一次请客带我们去清华墙外的一个晒麦场，7元钱一斤的涮羊肉吃得人人肚圆。一个会玩会吃的牛人倘若又是年年清华年级第一，世界顶级的美国西北大学土木博士，那算不算一个super牛呢！顺便说一句，这小子口才和笔头功夫那也是一流的。倘若说一定要挑个毛病的话，如果他觉得他有道理，他是绝对不会低头的。还有就是，他爱脸红。<br />
<br />
―――“睡梦罗汉”<br />
