20091120 18:44:12&nbsp; &nbsp;&nbsp; &nbsp;<br />
 <br />
（中央社记者周慧盈台北特稿）中国总理温家宝日前在官方一项座谈会告诉官员要勇于建言，并强调言者无罪。这番「言者无罪」的说法最近慢慢发酵，有民众直言，如果中共真能做到，中国就有希望成为真正的大国。<br />
<br />
中国大陆的言论自由一向受到国际社会诟病，人权组织的批判更从未间断过。<br />
<br />
而大陆国内因言获罪的事件更未曾稍歇，主张人权与民主的政治异议人士前仆后继因各种罪名下狱，去年底以来最受外界瞩目的案例是，刘晓波因起草连署「零八宪章」，被冠以「涉嫌煽动颠覆国家政权罪」遭逮捕。<br />
<br />
就在刘晓波仍深陷牢灾之际，温家宝日前在向新聘任的国务院参事、中央文史研究馆馆员颁发聘书时，请新任参事、馆员针对政府工作发表意见，并且「知无不言、言无不尽、言者无罪、闻者足戒」。<br />
<br />
温家宝讲话的对象虽然是政府官员，却让一些大陆民众心生期待。<br />
<br />
有人投书西方媒体说，中国现在特别需要「言者无罪」，如果中国共产党真能做到这一条，中国就大有希望成为真正的大国家、好国家、强国家。<br />
<br />
民众更说，如果中共当局能做到「言者无罪」，中国共产党也就大有希望，能改革成为一个为人民服务的大党、好党、正确的党。<br />
<br />
还有人在官方新华网发表文章称，「温总理的一席话，让作为媒体从业者的笔者感动。」<br />
<br />
文章表示，「如果官员都能像温总理那样『闻过则喜』，就不会出现一些『因言获罪』事件了。」<br />
<br />
舆论还认为，中国若能做能「言者无罪」，首先能解决的大事就是反腐。<br />
<br />
对温家宝的讲话有所期望的民众说，「中国现在官员的腐败，已经到了非治不可的地步，尤其是专政部门的腐败、法制部门的腐败，如能用人民的言论来监督，肯定能触动腐败的根源，有实效的解决腐败大恶。」<br />
<br />
在期待之余，舆论也表示，在现阶段的中国大陆要做到「言者无罪」谈何容易。真正有心要做，首先就要立法规定「言者无罪」的具体条文，严格禁止「颠覆国家罪」、「危害国家安全罪」以及「泄露国家机密罪」，更要杜绝司法人员当前无法无天的执法恶习。<br />
<br />
