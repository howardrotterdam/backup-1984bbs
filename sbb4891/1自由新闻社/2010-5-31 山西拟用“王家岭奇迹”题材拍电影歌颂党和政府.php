<br />
　　影片聘请曾创作电影剧本《生死抉择》《寻找微尘》的贺子壮担任编剧，并由曾获电影百合奖的优秀导演高峰执导。<br />
<br />
　　省煤炭地质局副总工程师李振拴、汾西救护大队党委书记宋承文、阳煤救护大队大队长赵青云、同煤救护大队副大队长马红利，均是剧本原型。他们与省煤炭厅、卫生厅、安监局等部门参与“3・28”大救援的人员，共同完成了对《八天八夜》剧本的研讨。<br />
<br />
　　本片将由山西电影制片厂创作、摄制，预计投资1000万元。这不仅是山西厂史上投资最大的影片，也是该厂转企改制以来第一部电影故事片。影片除了聘请国内一线主创阵容外，还将斥资搭建井下作业场景、运用三维动画技术，再现煤矿井下作业场面与透水肆虐的场景。<br />
<br />
　　据悉，影片将作为今年国庆重点献礼片在“十一”前后隆重推出。<br />
