																											<div id="postmessage_650892" class="t_msgfont">　　暂定名《八天八夜》，将由山西电影制片厂创作、摄制<br />
<br />
　　“王家岭大救援”创造了煤矿救援史的奇迹！它即将被山西电影制片厂搬上银幕，暂定名《八天八夜》。29日，本片导演、编剧携剧本初稿来到太原，举办剧本研讨会，特别邀请全程参加抢险救援的一线救援人员提出修改意见。<br />
