　　记者从宴会上的菜谱看到，主菜是四菜一汤，分别是：干贝银丝汤、清炒虾球、酱烧小牛排、茭白鲜蔬、柠香银鳕鱼。主食是扬州炒饭，点心，最后还有水果。 <br />
<br />
　　人民大会堂国宴菜谱融合了传统中国八大菜系的精华，据人大会堂国宴厅首任总厨孙应武日前透露，人民大会堂做出的宴会菜，讲究的并不是原汁原味的地道，而是有所改良。因为要考虑到宾客来自全国各地甚至世界各地，口味差异很大，国宴要调和出大家都能够接受的味道。因此人民大会堂国宴基本原则有两条，一是“好吃不如爱吃”，二是“可操作性”。 <br />
<br />
　　记者还发现，国宴席上配备了西餐和中餐两种餐具，这是为了适应中外嘉宾的不同饮食习惯。记者发现，先上桌的冷菜颇受外国嘉宾喜欢，很多盘子清空了。 <br />
<br />
　　能参加国宴是一种荣誉。记者看到，许多宾客穿着本民族特色盛装而来，有的模范代表甚至胸佩十多个勋章赴宴。中华总商会常务副会长、休斯顿中国和平统一促进会会长许华章表示，作为海外侨胞代表很荣幸获邀出席国宴。 记者 王辛莉 周兆军</div>

							
							
							
							
