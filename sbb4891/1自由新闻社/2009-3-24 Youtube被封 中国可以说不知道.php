　　中国现代国际关系研究院研究员唐岚向《环球时报》记者表示，世界各国都有管理网络的法规，比如，欧盟就将危害国家安全、煽动民族对立、传播色情、暴力等内容视为非法信息。而YouTube因播出不当视频，此前就有被多国“临时审查”的记录。比如：2007年3月，因上传侮辱土耳其国父凯末尔的视频，被土耳其政府封杀；同年4月，因播出侮辱泰国国王的视频而被泰国政府封杀4个月；2008 年，YouTube因亵渎伊斯兰教在巴基斯坦也被封杀。据《环球时报》记者了解，YouTube在土耳其至今仍未解禁。<br />
