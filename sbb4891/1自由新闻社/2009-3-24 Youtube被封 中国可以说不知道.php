																											<div id="postmessage_60687" class="t_msgfont">这是来自外交部例行记者会实录的消息。2009年3月24日的外交部例行记者会（节选）：　　　　<br />
问：据说Youtbe视频网站在中国境内被屏蔽。请证实。你能否介绍中国有关互联网的法律和管理规定？　　　　<br />
答：中国政府不害怕互联网，否则你怎么解释以下一组数字：目前中国网民数量达3亿，居世界第一，较8年前增长了30倍；中国的网站总数达210万，较8年前增长了138倍；博客空间超过1亿，比英国的人口还多。这些足以证明中国的互联网是充分开放的。我们鼓励积极地使用互联网，但同时也对互联网依法进行管理。世界上许多国家都依法对网络进行管理，防止对国家安全和社会公众利益有害的信息传播。在这些方面，我们也借鉴了其他一些国家的作法。我可以举一些例子。美国有联邦一级的立法，1998年《儿童在线隐私保护法》，1998年《数字千年版权法》，1999年、2000年、2003年美国国会分别通过了《反域名名称抢注法》、《消费者保护法》、《未成年人互联网保护法》、《反垃圾邮件法》。2001年“9.11”事件以后，美国的《爱国者法》有关条款也有涉及互联网管理方面的内容。《爱国者法》规定，“如出现危及国家安全的情况，当局无需事先征得法院的同意，即可监视电子邮件和其它互联网上的相关信息。” 　　　　<br />
追问：是否可以证实Youtube被中国有关当局屏蔽了？　　　　<br />
答：我可以告诉你的是，中国政府依法对网络进行管理。<br />
即使网民可以通过翻墙的办法登录youtube,但视频还是无法看到，YOUTBE又要失去众多的来自中国的流量了，期待着下次的解封吧。</div>

							
							
							
							
