<br />
　　矿泉水喷灭东方明珠火灾<br />
<br />
　　本月13日零点55分左右，东方明珠广播电视塔塔端发射天线遭强雷击，引起天线外罩燃烧，本报曾对此进行报道。上海市消防局参谋长周建中大校昨天介绍，由于起火点的位置特殊，曲臂消防车也无法企及，为尽快扑灭明火，龙阳特勤消防队的王永强等两名消防队员佩戴上防护设施，爬上天线，试图用干粉灭火器扑救，却并未成功。这时，下方的战友给两人递来了瓶装矿泉水解渴，两个消防战士灵机一动，将矿泉水含在口中，朝着起火点喷射，最终将明火遏制住。<br />
<br />
　　记者了解到，在此次灭火行动中立功的王永强曾在映秀镇的地震救灾中救出12条生命，被公安部消防局记一等功。<br />
<br />
　　火势最终被顺利扑灭，而此类超高层建筑的扑救和救援工作却再次成为人们关注的话题。周建中大校介绍，上海消防部门拥有的最高的曲臂消防车也只能满足90米高度的火灾扑救，而且欠缺操作面，因此在东方明珠电视塔的火灾扑救上起不到太大作用，考虑到超高层灭火救援的需求，消防部门正在打算购买两台消防专用直升机，在灭火时，直升机可以悬停在空中，用水炮轰打扑救。<br />
<br />
　　市应急救援总队成立<br />
<br />
　　记者了解到，昨天成立的市应急救援总队依托上海市公安消防总队组建，区、县应急救援支队依托区、县公安消防支队组建，实行“一支队伍、两块牌子”的运作模式，承担以抢救人员生命为主的应急救援任务，并负责现场的统一救援指挥。在昨天的成立仪式上，大量先进的救援车辆和设备亮相。<br />
<br />
　　拥有两个驾驶室的双向驾驶抢险救援压缩空气泡沫消防车可以双向行驶，用于隧道烟雾环境中作战的需要，在不熄灭发动机的情况下可实现一键切换驾驶，在转弯时前后桥分别向相反方向同时转向达45度，可以实现转弯半径为8米。这是亚洲第一辆、世界第四辆双向驾驶消防车。<br />
<br />
