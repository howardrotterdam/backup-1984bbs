爱吃麻虾的还真不少，本人原来也深好此道，但自打和天津创业环保总工吃了一次饭后，就再也没吃过那东西。(备注：创业环保主业是污水处理，原身是渤海化工， 炒股的人肯定知道。) 该总工说，麻小是日本人培育出来的，目的是用其处理城市污水，而且是首道处理程序的重要参与者。水越脏、重金属含量越高，麻小活得越滋润，水越干净，麻小死得越快。后来，麻小被引进中国，创业环保也在使用这种&quot;重金属清洁剂&quot;。那顿饭，只吃了一半，便草草收场。我坚决抵制，但是今年的麻小特别红火，到处都是，前天在密云路的那家我就亲眼目睹过，当时我在等菜，就听见好多人在点，而且都吃的有滋有味的，真的很难想象。。。<br />
<br />
各位，拜托千万别吃小龙虾了。它生长在地沟里，生长环境越恶略，越污脏，它的生命力越旺盛。现在的小龙虾都是人工养殖的，但是你们见过小龙虾的养殖环境么？为了让小龙虾长的又大又肥，缩短它的生长期，满足因为吃不起海鲜而自欺欺人的可怜的内陆人的馋瘾，那些黑心的&quot;养虾人&quot;往池子里用大皮管子灌化学铅、 汞和生活垃圾，更甚的还有金属油，而经过小龙虾的不断吸收营养后，那本来从皮管子里输出的黑垃圾，在池子里却是越来越清净，越是肥的龙虾，就越是肥硕。千真万确，我的一个朋友在新华网，他秘密跟踪拍摄了照片，希望发稿告诉大家，结果被新华网的主编截住了。后来这个朋友一赌气说, 晚上去簋街请主编吃麻小，你猜主编说什麽：你想害我就直说呀！说完连连做呕吐状。<br />
<br />
珍惜生命是我们说得最多的话，难道我们没有看够象躲避瘟疫一样投来的鄙视的目光么？麻小就是喇咕肺吸虫的中间宿主。<br />
