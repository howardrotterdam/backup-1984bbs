													<h2>南京多人疑食用小龙虾致肌肉溶解</h2>
						
						
																											<div id="postmessage_793330" class="t_msgfont"><a href="http://news.163.com/10/0823/03/6EO7I86T00014AED.html" target="_blank">http://news.163.com/10/0823/03/6EO7I86T00014AED.html</a><br />
<br />
核心提示：今年七月份开始，南京鼓楼医院急诊科也陆续收治了因食用龙虾而入院的病人。这些患者并没有呕吐腹泻等胃肠道症状，而均表现为肌肉酸痛难忍，医生诊断为横纹肌溶解症。情况严重的还因肾功能受损，出现了“酱油尿”。经调查，或因&quot;洗虾粉&quot;致病。<br />
<br />
<br />
扬子晚报8月23日报道&nbsp;&nbsp;腰酸、背痛，脸色煞白，站都站不起来――21日下午，当27岁的程小姐从江苏省人民医院急诊科转入肾科病房的时候，肾科刘佳主任医师还以为来了什么重病号。问清情况之后才知道，原来又是祸起龙虾。“这已经是这两天病房收治的第三个了。”刘佳主任医师说。记者了解到，这一星期以来，省人民医院收治的因食用龙虾出现身体问题的，已经达到了十多人。<br />
<br />
省人民医院收治的这些病人并不是特例。从今年七月份开始，南京鼓楼医院急诊科也陆续收治了因食用龙虾而入院的病人。与普通龙虾带来的食物中毒不同的是，这些患者并没有呕吐腹泻等胃肠道症状，而均表现为肌肉酸痛难忍，医生诊断为横纹肌溶解症。情况严重的还因肾功能受损，出现了“酱油尿”。<br />
<br />
最新病例　4人吃同一家饭店龙虾，3人倒了<br />
<br />
在江苏省人民医院肾科病房，小宫躺在病床上玩着游戏机。“已经没有问题了，医生说要留院观察一下，看是不是有肾功能损伤。”小宫今年21岁。她告诉记者，自己是在20日凌晨一两点吃的龙虾。“爸爸和朋友一起去饭店吃饭，给我和妈妈打包回来的，我大概吃了十来只吧，还喝了点啤酒。”早上7点左右，小宫就开始觉得全身酸痛，连呼吸也不是那么顺畅，小宫妈妈出现症状的时间还要更早一点。两人被送到了省人民医院急诊科。“爸爸没有事，但爸爸的朋友也出现了问题，去了另外一家医院。”<br />
<br />
因为两人病情严重，所以从急诊科转到肾科。检查结果显示，两人血液中肌红蛋白分别高达4000μg/L和6000μg/L。“正常人这个数值不会超过50，如此高的肌红蛋白说明这对母女的肌肉出现了溶解。”<br />
<br />
与小宫母女不同的是，27岁的程小姐食用的龙虾是买来自己加工的。在食用龙虾仅仅几个小时之后，程小姐就出现了全身酸痛的症状，“背痛得非常厉害，像针刺一样。”此外程小姐还觉得呼吸有些困难，但没有一般食物中毒之后恶心、呕吐、拉肚子的症状。<br />
<br />
在被送到省人民医院后，医生问清病史之后初步确认与曾经食用龙虾有关。检查显示，程小姐的肌红蛋白在1000μg/L左右，但已经出现了尿蛋白，因此肾脏功能受到了影响。<br />
<br />
病情释疑　肌肉怎么会溶解？<br />
<br />
在小宫的病历上，记者看到医生诊断为“肌红蛋白血症”，而在鼓楼医院则诊断为横纹肌溶解症。刘佳主任医师表示，这两种诊断其实是相同的，因为肌肉溶解，病人血液中才出现了高肌红蛋白。<br />
<br />
刘佳主任告诉记者，一般来说，肌肉溶解主要是由于挤压、外伤、剧烈运动等因素造成的肌肉损伤引起的，此外一些对肌肉产生损害的病毒、降脂药、β2受体激动剂、苯丙胺、毒品等药物也会使肌肉溶解。因为肌肉细胞膜被破坏，使得肌肉细胞中的内容物被释放到血液中，这些内容物就包括大量的肌红蛋白。由于肌红蛋白需要通过肾脏来排出体外，而肌红蛋白本身就是个“大个子”，所以很容易阻塞肾小管，影响肾功能。症状较轻的会产生血红蛋白尿，严重的还会引起急性肾衰竭。<br />
<br />
对肌红蛋白血症患者，医生会采用碱化尿液处理，并给患者输液、让患者多饮水，以帮助肌红蛋白排出体外。一些不是特别严重的肾脏损伤也是可以逆转的。<br />
<br />
“祸首”追究　用排除法推断，洗虾粉嫌疑最大<br />
<br />
根据省人民医院的急诊记录，一周以来已收治因食用龙虾而出现肌肉酸痛、僵硬症状的病人十多人。这些病人以中青年人为主，分布上比较分散，既有在饭店食用加工好的龙虾，也有买了龙虾回家自己加工的。除南京外，还有个别病人是从外地转过来的。<br />
<br />
“从流行病学的调查方法，我们收治的病人出现食用过龙虾的症状，从食用龙虾到发病的时间差不多，症状也相同，所以我们可以确定，收治肌红蛋白血症的病人和曾经食用龙虾有关。”刘佳主任这样表示。<br />
<br />
可以确定“肇事”的是龙虾，但以往龙虾食物中毒多表现为呕吐、腹泻等肠道症状，这次病例则是肌肉溶解，情况明显不同。多名病例集中出现，这在以往也是没有的。“龙虾生存的环境出了问题、龙虾在运输过程中受到了污染，在清洁、烹制的过程中出了问题，都有可能导致这种结果。”<br />
<br />
最先发现“龙虾病例”的鼓楼医院急诊检验科医生也一直都在追踪这些病例，并且发现其中还存在着不少疑点。鼓楼医院接诊的病例所食用的龙虾基本上是自家烧煮的，且是红烧，所以细菌性食物中毒的可能性不大。“重金属残留说”也可以基本被否定，因为重金属残留对人体的伤害是积累下来的，也不会立即表现出来，相比较化学物质中毒、有机中毒的可能性更大。<br />
<br />
几位患者的描述让医生们有了点线索，他们描述的龙虾都是特别大，看上去特别干净。为此医生下班后还专门问了小区附近的一个卖龙虾的小贩。小贩承认自己的龙虾是洗虾粉洗的，但是具体洗虾粉是什么成分，小贩也说不上来。<br />
<br />
健康提醒　掉钳的龙虾要慎食<br />
<br />
<br />
医生提醒，龙虾本身生存的环境藏污纳垢，龙虾头部又是最容易吸附重金属和细菌的部位，所以食用小龙虾，最好要去头。活龙虾在买来后，最好放在清水里养24-36小时，使其吐净体内的泥沙等杂质。在加工龙虾时，它两鳃里的脏东西要清除，因为鳃毛里面吸附了很多细菌，最好是把鳃剪掉，虾壳最好用刷子刷洗。龙虾细爪的根部最容易藏污纳垢，一定要剪掉。最后还要经过刷、洗才能烹饪。<br />
<br />
另外，龙虾是否用洗虾粉洗过靠吃是无法品尝出的，但可以通过观察来鉴别。用洗虾粉洗过的小龙虾，往往看上去非常干净，色泽也很光鲜。但是被腐蚀过后的小龙虾虾钳很容易脱落，如果餐桌上的小龙虾虾钳普遍比较少的话，使用洗虾粉的可能性就比较大。<br />
<br />
