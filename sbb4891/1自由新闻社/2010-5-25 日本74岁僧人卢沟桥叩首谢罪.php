<br />
<img src="http://res.news.ifeng.com/attachments/2010/05/25/rd_or_22928fae74b74eb9e61446fe7c9abebc.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<img src="http://res.news.ifeng.com/attachments/2010/05/25/rd_or_6f8d816bd01b643da43662679fa8b365.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
岩田隆造来到卢沟桥上以谢罪行动表达对日军侵华战争罪行的忏悔。<br />
<br />
昨日下午，卢沟桥上，74岁的日本僧人岩田隆造，连续用“下跪叩首”等动作，表达对七十多年前日军侵华战争罪行的忏悔。<br />
<br />
昨日14时30分许，岩田老人来到卢沟桥。他身体瘦小，白色僧袍和橙色袈裟很干净，身负着一个鼓鼓的双肩背包和两个布包。<br />
<br />
在桥上，岩田把两张写有“谢罪”的白布，摆在最显眼的位置。随后，他摊开手中的纸板。“三天绝食、日本僧、岩田”几个字分外醒目。岩田脱掉鞋子，跪下、叩首、起身、抬头仰望，双手合十、再次叩首，连续重复着这些的动作，表情平静而严肃。<br />
<br />
一名操东北口音的男子问岩田是否到过东北？岩田回答，他曾两次去过哈尔滨，也记得日军在东北犯下的罪行。看到一些小学生，岩田笑着说，在日本这个年龄的小孩比中国小孩矮多了，想必中国的孩子都知道那段历史。同时，他还拿出他四年前来卢沟桥谢罪的报道给游客看。<br />
<br />
离开前，老人面对卢沟桥，吟诵几句，深深鞠了几个躬。<br />
<br />
■ 对话<br />
<br />
对一些日本人做法感到失望<br />
<br />
新京报：你为什么会有来中国道歉的想法？<br />
<br />
岩田隆造：以前我们的课本里有日本侵华的内容，但是现在没有了。我想通过自己的行为来教育日本人。<br />
<br />
新京报：为什么如此执着地来中国谢罪？<br />
<br />
岩田隆造：中国是日本最大的邻国，希望中日建立友好关系，我想用自己不断的行动来表达我的坚持。<br />
<br />
新京报：日本国人赞同你的做法吗？<br />
<br />
岩田隆造：很多日本人对此既没有想法（概念）也没有行动，我感到很失望。右翼势力会威胁我们，拿着刀闯入寺庙威胁我们不让我们做这些事。日本僧人会支持和平，但他们也没有行动。<br />
<br />
新京报：在中国的生活怎样？<br />
<br />
岩田隆造：对我都挺好的。有些中国人给我食物和饮料，他们对我很好。（记者李天宇实习生邓雨露 韦璐明）</div>

							
							
							
							
