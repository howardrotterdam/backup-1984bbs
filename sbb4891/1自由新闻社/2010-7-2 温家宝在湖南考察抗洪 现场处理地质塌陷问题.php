													<h2>温家宝在湖南考察抗洪 现场处理地质塌陷问题</h2>
						
						
																											<div id="postmessage_717426" class="t_msgfont"><a href="http://www.sina.com.cn" target="_blank">http://www.sina.com.cn</a>&nbsp;&nbsp;2010年07月02日12:13&nbsp;&nbsp;中国广播网 <br />
　　中广网北京7月2日消息（记者刘乐）今年入汛以来，湖南省先后发生10次强降雨，湘江干流已连续三次出现全线超警戒水位，部分支流发生超历史记录洪水，造成较重的洪涝灾害。7月1日下午，中共中央政治局常委、国务院总理温家宝来到湖南考察防汛抗洪工作。<br />
<br />
　　温总理一下飞机，就乘车一个多小时来到宁乡县察看被冲毁的峨山坝。<br />
<br />
　　当地负责人：决坝的时候是在6月23日……<br />
<br />
　　河道两岸机械轰鸣，大家正在紧张抢修围堰。总理冒着35度的高温，登上损毁的堤坝察看询问抢修的进展情况。当地负责人说，要赶在7月15号晚稻种植前修复堤坝，为灌溉提供条件。温总理来到参加修复建设的武警和民兵面前对大家说：<br />
<br />
　　温家宝：我们必须一手抓抗洪、一手抓生产。只有粮食丰收了，经济稳定发展、调整结构和管好通胀预期才有基础。你们现在所做的工作关系着国家经济建设的大局。我代表党中央、国务院向同志们表示感谢和慰问！同志们辛苦了！<br />
<br />
　　武警和民兵预备役官兵：为人民服务！<br />
<br />
　　随后，温总理乘车离开大坝。途中有不少农民站在道路两旁向总理挥手致意。温总理随即走下车与农民们站在地头上聊了起来。这时，一个中年汉子挤到总理面前说：“温总理，我向您反映一个问题。”<br />
<br />
　　他叫陈凯旋，前几天下大雨，他们福泉村附近塌陷出一个大坑。大家都提心吊胆。<br />
<br />
　　温家宝：你反映的是一个大事情，涉及到群众的生命安全。<br />
<br />
　　受灾群众：我们那的学校都塌掉了。<br />
<br />
　　温家宝：学生们呢？<br />
<br />
　　受灾群众：学什么安全。<br />
<br />
　　温家宝：没发生问题吧？<br />
<br />
　　受灾群众：没有，没有。<br />
<br />
　　温家宝：坑有多深？<br />
<br />
　　受灾群众：有几十米深。<br />
<br />
　　温家宝：面积呢？<br />
<br />
　　……<br />
<br />
　　陈凯旋说想请总理过去看看。这时，当地负责人说，这件事已经制定了治理和安置方案，请总理放心。温总理还是拉着陈凯旋的手说：“来，上我的车，你这就带我去看看。”上车后，温总理让陈凯旋坐在身旁，对他说：“你当指挥，咱们到大坑那里去。”<br />
<br />
　　十几分钟后，车子来到村口。温总理下车让陈凯旋带路向大坑走去。沿着小道可以看到当地政府竖立着地质灾害沉陷区的警示标牌。走了500多米，温总理突然加快脚步爬上一个山坡，这时，一个直径近五十米，深三十多米的大陷坑赫然出现在眼前，大坑边缘的民房由于塌陷只剩下一半，大坑周围用竹竿围着。<br />
<br />
　　温总理不顾工作人员的劝阻，跨过竹竿走到坑边低头察看，脚下松软的土地随时都有再次塌陷的可能，大家都十分担心，但总理并不在意。听了村民们的介绍后温总理沉思片刻说：<br />
<br />
　　温家宝：因为我多少也是一个内行。几级领导都在这里，我们定下几件事，好吗？<br />
<br />
　　温总理非常干脆地说，要组成专家组，圈定塌陷区范围。要迅速转移塌陷区内群众，决不能发生人员伤亡；要妥善安排群众生产生活。这几件事情必须马上做。<br />
<br />
　　就要离开时，突然一位妇女在不远处哭了起来。<br />
<br />
　　温家宝：别叫她哭，叫她找我来，没关系。<br />
<br />
　　她是一个塌陷重灾户，希望政府能重视理赔问题。她递给总理一张纸条。温总理认真地看了起来。<br />
<br />
　　温家宝：你这上写着了：“我们每天日思夜想的，要是中央领导同志亲自来看看，那该多好啊！”我不是来了吗？意思我懂了，你的心情我很理解。我又给他们加上一项任务，我们一定不让受灾的群众在经济上再遭受更大的损失。<br />
<br />
　　在村口，温总理对乡亲们说：<br />
<br />
　　温家宝：我是看水利工程的。刚才群众告诉我有塌陷区，我就赶来了。我向在塌陷区受灾的群众表示慰问。请你们放心，处理塌陷区的地质灾害，我刚才已经作了部署：第一，迅速组织力量查明塌陷的原因和范围。第二，要把保护群众生命安全放在第一位，采取果断措施，转移群众，决不能发生一起群众人员伤亡的事故。第三，对于受灾群众，各级政府要合理进行补偿，帮助他们重建家园，恢复生产。第四，对于转移到安全地带的群众，一定要安排好他们的生产、生活。这四项工作都要落实责任，如果再发生因塌陷引起的人员伤亡，一定要追究各级领导的责任。<br />
<br />
　　温总理还嘱咐乡亲们，要做好自我保护工作。他最后对大家说：<br />
<br />
　　温家宝：我已经把工作做了部署，你们是不是可以放心了？<br />
<br />
　　受灾群众：放心！放心！谢谢总理！<br />
<br />
　　当晚，温家宝在宁乡县主持召开座谈会。他强调，要切实加强山洪、塌陷、滑坡、泥石流等灾害的防御。国土资源部门要抓紧对这些地质灾害进行普查，有计划、分步骤地开展治理。要教育群众切实增强避灾意识，认真落实受洪水威胁地区的人员转移安置措施，在第一时间预警，最大限度减少人员伤亡。</div>

							
							
							
							
