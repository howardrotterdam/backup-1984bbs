就要离开时，温家宝看到一位妇女在不远处啜泣，随即让工作人员把她叫到自己跟前。这位妇女递给总理一张纸条。温家宝接过来认真看着。<br />
<br />
<br />
　　她叫朱君，是一个塌陷重灾户。她家去年刚刚花十多万元装修了房屋，如今因为塌陷不但房子没了，连粮食、牲口都陷在坑里。她希望政府能重视补偿问题。<br />
<br />
<br />
　　温家宝拿着纸条念道：我每天日思夜想的是，如果中央领导亲自来看看，那该多好啊！<br />
<br />
<br />
　　总理念完后对她说：“你看，我不是来了吗？”<br />
<br />
<br />
　　一句话说得朱君破涕为笑。<br />
<br />
<br />
　　“你的意思我懂。刚才我讲的几件事情还应该再补充一条，群众房屋因塌陷倒塌的要合理补偿，一定不让受灾群众经济上再遭受更大损失。”温家宝对她说。<br />
<br />
<br />
　　在村口，温家宝对前来送行的众乡亲说，我本来是看水毁工程的。一位群众告诉我，这里有塌陷区，我就赶来了。我向在塌陷区受灾的群众表示慰问。处理塌陷区地质灾害我刚才已经作了部署，主要是做好四个方面的工作：第一，迅速查明塌陷的原因和范围，组织专家实地勘察。第二，务必始终把保护群众生命安全放在第一位，采取果断措施转移群众，决不能发生一起人员伤亡的事故。第三，对于因为塌陷造成损失的受灾群众，各级政府及相关企业要合理补偿，帮助他们重建家园，恢复生产。第四，要安排好转移群众的生产生活，特别是要让孩子们有一个安全的学习环境，让群众有安全的居住地点。这四项工作都要落实责任，如果因塌陷造成人员伤亡，一定要追究领导责任。温家宝还嘱咐乡亲们，要做好自我保护工作等。<br />
<br />
<br />
