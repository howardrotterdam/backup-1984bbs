笔者走到商业厅门口时，有5-6人坐在道路边石阶上，讨论，“二道桥被封锁了，根本过不去，回不了家了，说是店铺被砸，武警都出动了。”<br />
<br />
一路上不少百姓三三两两或站或坐在路边，议论发生的种种。这时手机仍在无法拨通。<br />
<br />
街上有防暴车徐徐行驶过去，警车、救护车时而呼啸而过。<br />
<br />
新华南路上，警察明显增多，天山大饭店等处十字路口均有警车和警察驻守。<br />
<br />
11点半，找到网络上网，发现固定电话仍可以使用，街道上时而传来救护车和警车的鸣笛。<br />
<br />
Qq群里，乌鲁木齐的同学朋友群里已开始讨论所见所闻的暴乱种种。并有上传图片和视频。<br />
<br />
可以点击打开的现场视频中，可以清晰的看到被焚烧的汽车、部分居民楼，也能听到枪声，但伤亡人数不详。维族聚居的二道桥附近是多事区域，疑似有人使用自制枪械，袭击汉族人住宅。但也可能是武警鸣枪示警。<br />
<br />
有朋友讨论说这次暴动跟6月26日广州韶关的2名维吾尔族工人死亡密切相关，维吾尔族人不满广州政府的处理结果，故引起骚动。<br />
<br />
大型港资企业韶关旭日玩具厂前晚发生汉族工人和新疆维族工人械斗案。由于厂内接连传出女工被维族工人强奸事件，逾百名汉族工人前晚手持铁棍冲入新疆人宿舍报复，数百人刀光剑影，血溅当场。暴乱持续至昨日凌晨，大批武警到场制止。冲突造成一百二十人受伤，两名维族工人死亡。<br />
<br />
有大批武警身着头盔、制服、道路边防暴车和警车等图片，也有上传被刀砍伤、触目惊心的维吾尔族男青年及韶关现场惨象、血迹斑斑的图片及视频。<br />
<br />
深夜12点半，乌鲁木齐市政府发布《关于维护社会正常秩序的紧急通告》，宣布在7月6日凌晨1时至8时（北京时间），由公安机关对乌鲁木齐市部分区域实行“交通管制”，在管制时间和区域内，任何车辆不得通行。<br />
<br />
晚1：30分，有朋友在网上说，她的姑姑住在光明路，说那边傍晚也发生了骚乱，她说她当时看到四处在焚烧，爆炸声都能听见，遍地是玻璃碎片，到处是血迹。并说已开入坦克。<br />
朋友的丈夫已被召回单位，全员戒严。<br />
<br />
