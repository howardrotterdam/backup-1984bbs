													<h2>《中国的污染》获人道主义摄影奖</h2>
						
						
																											<div id="postmessage_283904" class="t_msgfont"><a href="http://finance.ifeng.com/roll/20091013/1328692.shtml" target="_blank">http://finance.ifeng.com/roll/20091013/1328692.shtml</a><br />
<br />
2009年10月13日 19:10外滩画报<br />
<br />
<img src="http://img.ifeng.com/tres/finance//upload/images/2009/1013/191054/69_1328692_aaf33b04eaa06d4bd817f928e40ac273.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
卢广工作照<br />
<br />
2009 尤金。史密斯人道主义摄影奖得主卢广专访--“记录是第一位的”<br />
<br />
10月14日，尤金-史密斯人道主义摄影奖公布结果，获奖者是来自中国的摄影师卢广；凭借《中国的污染》专题，他成为中国内地受此嘉奖的第一人。在接受本报专访时，他表示，曾经自己摄影目标是为了钱和得奖；但现在，他指向了纪录中国历史，解决现实问题。“我现在考虑的就是怎么能好好的解决这些问题，解决不了也没关系，我先纪录，纪录对我来说是第一位的。”<br />
<br />
文/丁晓蕾 图/卢广<br />
<br />
每年，尤金-史密斯人道主义摄影奖颁发一个大奖和两个助研奖。 作为世界上最有声望的摄影奖项之一，该摄影奖关注那些像摄影大师尤金。史密斯一样，立志“以人道主义为主题，反映现实生活”的摄影师。此前，只有中国台湾摄影师、玛格南图片社成员张乾琦获得过该奖。<br />
<br />
2009年，卢广则成为中国内地第一人。卢广，1961年出生，浙江金华人，1995年开始接触纪实摄影。《中国的污染》是卢广自2005年开始拍摄的专题。近5年来，卢广的足迹遍布中国，从中国西部到东部沿海，从黄河流域到长江两岸，专题的内容不断壮大，真实的纪录了中国的污染现状。凭借该专题，卢广先是在2008年获得了尤金。史密斯助研奖；今年，他获得了年度尤金。史密斯大奖，以及3万美元的奖金，以帮助他继续调查中国的环境污染情况。<br />
<br />
作为中国少数的自由摄影师之一，卢广一年到头在全国各地拍照，这个月在内蒙，下个月可能就去了海南。接受《外滩画报》专访时，卢广正准备第二天启程去青岛。因为刚刚有当地群众打电话告诉他，一个工厂把通向深海、直径60厘米的排污管换成了直径1.5米的，他“得去看一看”。<br />
<br />
<strong>乌海往事</strong><br />
<br />
卢广的“污染之路”，始于内蒙古的乌海市。<br />
<br />
乌海位于内蒙古自治区的西南部，南部与宁夏接壤，是通向西部的重要交通枢纽。黄河流经乌海，拐了一个大弯，沙漠与河水相连，夕阳西下时，会是一幅长河落日的塞上美景。<br />
<br />
卢广最早一次去乌海是在1995年，为的是探访那里的私人小煤窑。乌海市附近有丰富的矿物资源，如煤炭、铁矿石、高岭土、硅石等。1995年，乌海市郊的公乌素矿区里私人小煤窑遍地，卢广“曝光”了小煤窑里安全性极差的工作情况，引发全国性关注。，在中央政府的敦促下，当地的小煤窑纷纷关闭了。<br />
<br />
2005年3月，卢广再次踏上了去往乌海市的火车，目的地仍然是公乌素矿区。在乘车去往矿区的路上，他发现远远的十几公里外烟囱林立，黄色的浓烟滚滚。这远非古诗里的绝非“大漠孤烟”的景象了。他立刻决定，到冒黄烟的地方去看一看。 卢广经调查得知，黄烟的源头位于多海市、鄂尔多斯市和阿拉善盟的交界处，就在109国道的旁边，那里建起了一大片工厂，而且以高能耗、高污染的炼钢厂、焦化厂、电石厂居多。方圆50公里的范围内，聚集了七、八个大大小小的工业园区。高大的烟囱随处可见，废气的排放没有经过任何处理就直接排向天空，废气污染严重影响了周围居民的生活。109国道边还耸立着一块牌子“欢迎光临中国最大的高耗能工业园”。<br />
<br />
在驱车沿109国道前往工业园区的途中，卢广遇到了正在一群在路边抗议的一些村民。他上前了解情况，他才得知这些村民来自附近的北山村，离村100米的地方正在大张旗鼓地盖焦化厂，村民的草原、耕地被占，却没有得到任何补偿，赖以生存的农业也因为污染无以为继，生命健康也受到了威胁。<br />
<br />
“村民们看到我是拿相机的人，都拥过来向我诉苦喊冤，有的甚至跪下来。”卢广说。在副村长袁广深的带领下，卢广来到了北山村，尽管心里已经有所准备，他还是被眼前环境的恶劣景象情况惊呆了：村子被工厂包围，刺鼻的臭味随着空气的流动四处蔓延，目光所到之处都是灰蒙蒙的一片。<br />
<br />
没人想得到这里也曾有过绿草蓝天的草原景色。 原来，乌海市水土光热资源丰富，黄河流经市区长达105公里，昼夜温差大，日照时间长，非常适合蔬菜瓜果的生产。而自从污染加重后，北山村里的小麦产量已经下降了三分之一，西红柿、茄子等瓜果出现了黑点和腐烂现象，蔬菜也销售不出去了。<br />
<br />
从2003年开始，村里陆续有人患上癌症等疾病死去，而呼吸道疾病则几乎人人都有。袁广深告诉卢广：“如果赶上刮风天还好些，否则满屋子都是呛鼻子的味道。晚上睡觉前，必须紧闭门窗，但早晨起来脸还是黑黑的。”<br />
<br />
这些情况，村民都曾如实写在了寄给政府的抗议书里，但却迟迟得不到回应。带着照相机、“北京来的记者”卢广成了他们的希望。卢广带着村民们沉甸甸的投诉信回到了北京，连同10张在当地工厂和村子里拍摄的照片，一起寄给了国家环保总局。两周以后，当地村民接到了环保总局的调查电话，之后不久，环保总局派了工作小组到当地考察，责令当地政府整改。<br />
<br />
抗争的结果是工业园区与北山村签订了一份赔偿协议。根据这份协议，北山村被占用的草场将获得每亩333元、总共近630万元的赔偿，分15年付清。按照这份协议，村里的房子也将被拆除。北山村村民将全部搬走，这样，这个在30年前因工业发展而兴建起来的村子，又因为工业发展而消亡了。<br />
<br />
2006年4月，卢广再去北山村时，只剩下搬迁后的满目狼藉。北山村是公乌素镇唯一的以农业生产为主的村子，离开耕种的土地，村民们只好到镇子上打工为生。原先领着村民抗议的副村长袁广深，也只好去煤窑挖煤养家。 “我觉得有点对不起他们，”卢广说。<br />
<br />
<strong>跟踪中国工业污染之路</strong><br />
<br />
在向尤金。史密斯基金会提交的文字资料中，卢广写道：<br />
<br />
“一些地方政府有着错误的思想理论，比如‘发展经济总有污染的不要大惊小怪’，‘先发展后治理’这种谬论。我将把这几年所拍摄的环境污染状况、因污染引起的癌症高发区、畸形儿出生率增加等图片，通过媒体、互联网、展览等途径让更多的人了解真相，让大家都来关注中国的污染问题，特别是让地方政府部门官员和污染企业主看到这些触目惊心的环境污染和受害群众因污染身患绝症的图片，让他们认识到自己的错误，改变一些地方政府官员的观念，尽快从“以环境换效益”的短期思维中走出来，提高防范意识。 ”<br />
<br />
乌海市的问题告一段落后，卢广开始把主要精力便投入到了对中国污染状况的拍摄中。他经过调查发现，中国海洋和水域污染也是严重时期，主要分布在渤海、黄海、东海三大海域和长江、黄河、珠江、淮河、辽河、松花江七大河流水系中。最近几年，全国已有21326家化工企业，其中10000家在长江流域，4000家在黄河流域，2000家在人口稠密区和饮水源地。化工污染成为中国大江、大河、大海生态环境的重要“杀手”。<br />
<br />
于是，从乌海所处的西部黄河中上游沿岸开始，卢广的足迹逐渐向中部、东部扩展。5年下来，他几乎走遍全国。今年他向尤金。史密斯摄影奖评委会提交的作品里包含四十张照片，拍摄地点包括内蒙古、宁夏、山西、安徽、河南、江苏、浙江、云南、广东等地，覆盖了中国的大部分地区。他的目标是，“在今后二至五年时间，继续重点对渤海、东海、珠江、淮河、辽河、松花江进行调查拍摄，跟踪报道这些地区因工业污染破坏生态环境带来的严重后果，对中国工业污染现状有一个全面的调查。”<br />
<br />
所到之处， “居民是最直接的受害者，希望情况有所改善的愿望也最迫切。他们会主动带着我去找污染源，”卢广告诉记者说。然而，拍到了照片，不意味着污染问题能得到解决。卢广无奈地说起自己在浙江的经历：“浙江省为了监督污染，在一些污染严重的工厂的排污口装了摄像头，24小时监控。我后来去看，果然有所改善，觉得很高兴。但是不久后有群众告诉我，如果晚上和周末来，就会发现污水源源不断地流出来，因为，那是监控员下班休息的时候。”<br />
<br />
这种“上有政策、下有对策”的情况屡屡发生，让卢广也感到无奈，“怎么办呢？还得拍。”但在坚持拍摄之余，卢广开始寻求更多的解决办法。几年下来，卢广跟很多省市环保局的工作人员，以及很多大型工业企业的老板“打过照面”。他把自己拍的照片拿给他们看，希望通过自己的照片，让更多的人意识到事态的严重，让更多有能力帮忙、却因为不清楚事实而没有帮忙的人伸出援手。<br />
<br />
<strong>梦想越长越大</strong><br />
<br />
卢广的老家在浙江金华， 19岁，他就进入永康丝绸厂，成了一名染色工人。1980年，一次偶然的机会，他跟一个有相机的朋友一起，去附近一个风景秀丽的地方游玩拍照。卢广也试着拍了一张，结果比别人拍得都好。卢广有着浙江人特有的勤奋和精明，他把这张照片拿去卖，赚了4分钱。拍照能赚钱？这一下激发了卢广的兴趣，也是他开始钻研摄影的真正原因。 1980年，通过零零散散的卖风景照，卢广年收入达到了60块钱，当时他一个月的工资也不过17块。卢广很高兴，立刻用这笔钱买了一台红梅牌相机――这是第一台属于卢广自己的相机。<br />
<br />
1981年春节的时候，卢广趁着放假，带上相机、相纸和药水，骑着自行车来到35公里以外的偏僻村庄，住在一个熟人家里，简单布景之后，他开始给村里的人拍全家福。白天拍照，晚上洗照片，就这样从年三十到年初六，卢广赚了260块钱。<br />
<br />
“不得了，超过我一年的工资了，”卢广回忆道，“回到永康，我又马上去买了一台海鸥120的相机，花了120块钱。”接下来几年，卢广都一边在丝绸厂上班，一边用业余时间拍照赚钱。<br />
<br />
1986年，不到26岁的卢广成了永康市的政协委员，政府的人看到卢广有拍照的技能，介绍他到工人文化宫的图片社工作。1987年，文化宫的领导找到卢广，说图片社总是亏损，让他看看能不能想办法承包下来，卢广答应了。卢广承包图片社的第一个月，图片社还在亏钱，但第二个月就已收支平衡，第三个月则小有盈利了。两年以后，文化宫将图片社收回，卢广自立门户，开设了“鲁光照相馆”，拍一些“艺术沙龙照”，生意红火。虽然卢广还在文化宫上班，但那时的他已经看不上当时一个月七八十块的工资了。<br />
<br />
1993年8月，已经有十几万元家底的卢广又做出了惊人之举。他辞掉公职，把照相馆交给妹妹经营，自费赴北京中央工艺美术学院摄影研习班(现为清华大学美术学院)，开始了为期两年的进修学习。原本摆在卢广面前的有两个选择，要么北上北京，要么南下深圳。卢广解释自己选择北京的原因：“当时深圳特区发展很好，去那里肯定能赚钱，但是我出门的目的不是赚钱，而是学习。我的文化水平不好，更需要学习。” 卢广抱着“三年之内加入中国摄影家协会”的远大目标到了北京，“当时的摄影家协会在全国只有几千个人，能成为其中一员是每个搞摄影的人的梦想。” 1995年，他就被梦寐以求的摄影家协会批准为会员，提前实现了目标。<br />
<br />
研习班本来每期只有半年，卢广却一直在这个班里当“大班长”，一边学习，一边做一些助教的工作，一边继续拍照。“我这个人没别的，就是勤奋，别人去一趟，我去好几躺，直到在最佳的光线下拍到最好的照片为止”，卢广说。勤奋、渴望成功和名誉，卢广开始频繁地的参加比赛，频繁获奖。<br />
<br />
有老师指点卢广，你不能总是这样零零散散的拍，要静心做些自己的东西。卢广听了觉得很有道理，思考一番后，他决定要像摄影大师安瑟。亚当斯那样，拍摄大画幅的风光照。结果一问才知，一台大画幅相机要十几万。“我当时带着十万块钱到北京来，买了两套相机就已经花去了一半，再加上各种费用，根本没钱再买相机，大画幅的计划也只好停下来，”卢广说。<br />
<br />
1994年，卢广看到一本关于西部淘金的书，心中向往，于是前去拍摄，这组照片引起了强烈反响。卢广说：“我也好像一下找到了路，当时周围的人都在拍唯美照，艺术照，而纪实摄影却是少有人涉及的领域。”他接下来又把目光转向了吸毒者，别人听说后纷纷劝他，说吸毒者非常危险，最好不要拍。“我想到了就要做，谁劝也没用，”卢广不但到云南拍了吸毒者，还去拍了妓女，去山西拍了小煤窑。凭借这些重量级的专题，卢广获得了1996年中国摄影艺术的最高奖――金像奖。<br />
<br />
<strong>“我的责任是让他们知道”</strong><br />
<br />
虽然从1993年到北京，开始接触纪实摄影起，卢广拍摄的题材就大多是反映敏感话题、边缘群体，但他的拍摄态度却在2001年发生过一次巨大的转变。<br />
<br />
1996年以后，卢广从北京回了老家，原因很简单，“没钱了”。虽然获奖不断，但奖金远远不够卢广的开支；也有朋友主动提供住处给卢广，但是卢广觉得：“我也不是不会赚钱，相反，我还挺会赚钱。等我赚了钱，回来买个大房子，让朋友们都来我这里住。”抱着这样的念头，卢广回了金华，继续做照相馆的生意，生意做得很好，规模也越来越大，还开了一个广告公司。从1996年到2001年，卢广拼命赚钱，到了2001年上半年，卢广觉得“差不多了”，立刻停下生意，把公司交给别人，重回摄影界，再回北京。卢广实现了当初的目标，买了150平的房子。装修了两个月，把家安置好以后，卢广开始考虑下一个选题。<br />
<br />
“偶然一次机会，我从报纸的一角看到关于河南艾滋病村的报道，我觉得这个选题值得拍，第二天就启程去了。”当时卢广还没想到，这次出行将如何改变他的生活。<br />
<br />
“在去艾滋病村以前，我拍摄是为了自己的兴趣，为了赚钱，为了获奖。而但经过2001年，我变了，”卢广说。他人的苦痛曾是卢广的独特题材、获奖法宝，而从那之后，卢广开始试着分担拍摄对象的苦痛，试着去帮助他们，“比如有10个人，其中2两个需要被帮助，而另外另8个人虽有心帮，也有力帮，但他们不知道这2个人的存在。我的责任就是让他们知道。”<br />
<br />
2001年前后拍摄河南艾滋病村，是对卢广有重要意义的分水岭。在艾滋病村，卢广目睹“年迈的老母亲看着儿子躺在床上慢慢死去”、目睹“有个家庭总共八个人，有七个人感染了艾滋病，一家人就绝望的等死”……<br />
<br />
“那天晚上，我一夜没睡”，卢广他被这些巨大的苦痛所震动，第一次深切的感受到责任在肩，无可推卸。除了把这些照片拍好，公布出去，让大家知道艾滋病引发的灾难，他想不到自己还能做什么。从此“为弱势群体说话”，成了卢广从事摄影的新使命。2003年，凭《艾滋病村》，卢广获得了世界新闻摄影比赛(“荷赛”)的一等奖。<br />
<br />
<strong>“纪录是第一位的”</strong><br />
<br />
2001年以后，每年365天，卢广有200多天都在外奔。每次他几乎都是独自轻装上阵，带一个机身，两个镜头。主要的费用除了来回的路费，还有在拍摄当地租车的费用。由于污染专题的涉及面广，卢广比起一开始要现在跑的多，跑的远。之前做过的选题，比如云南吸毒者、河南艾滋病村，他都还在跟，一年至少会去一次。一年下来，路费花销要十几万，都是他自己承担。<br />
<br />
作为中国少数的自由摄影师之一，卢广曾拒绝过很多媒体的工作邀约，而是刻意保持着自由摄影师的身份，“媒体的摄影记者很难做到像我这样，花上几年甚至十几年的时间，把一个重大问题做深做透。”<br />
<br />
尽管一年到头在全国各地拍照，但每到春节的时候，他一定得回浙江的老家永康，经营自家的影楼生意，这是他拍摄经费的主要来源，除此之外，奖金和稿费也是收入之一。<br />
<br />
现在，卢广成为中国内地第一个得到尤金。史密斯人道主义摄影奖的摄影师，但这份殊荣却并不像以前那样，能给他带来莫大的满足感。用卢广自己的话说，现在的他已不再那么渴求得到“奖”的肯定，而是把更多的精力和心思放在如何拍摄更多好照片，如何更多的帮助别人上。而这种想法似乎正与尤金。史密斯基金会设立奖项的初衷相符。<br />
<br />
从2001年起，卢广还建立了名为“阳光聚焦网”的图片网站，现在主要由卢广的妻子徐小莉负责。妻子徐小莉是卢广的大后方，主持阳光聚焦网的同时，也负担起了大部分家庭的责任。她抱怨卢广是个工作狂，对家庭顾及得少，“他每天想的都是摄影，饭后散步都觉得是浪费生命。”<br />
<br />
阳光聚焦网一开始是放卢广自己的作品，后来成为朋友们展示作品的平台，现在共有一百多人，“作为一个大家展示作品的地方，让那些没有个人网站的业余摄影师的作品也能被人看到，”卢广说，“有些人做的好了，规模做大了，就自己去建立个人网站，我觉得挺好。” 对于现实生活，卢广觉得自己体力似乎有所下降。“以前都是天一亮出门，天黑了才回，一整天在外面跑都不累。而现在，”卢广连说力不从心，“虽然出门时还是很早，但中午都要回住所休息一下，下午才有经历继续干活。”<br />
<br />
卢广说，“我的拍摄追求的是结果。”曾经，结果指的是金钱，是得奖；但现在，结果指的是纪录中国历史，指的是解决现实问题。“我现在考虑的就是怎么能好好的解决这些问题，解决不了也没关系，我先纪录，纪录对我来说是第一位的。”</div>

							
							
							
							
