													<h2>天朝举人秀才衙役庶民等500人演绎新版公车上书</h2>
						
						
																											<div id="postmessage_208720" class="t_msgfont">500名官员学者上书中央，要求9常委尊纪守法 <br />
<br />
<br />
Hu Jintao总书记并中共中央政治局各位委员，<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;Wu Bangguo委员长并全国人大常委各位委员， <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; Jia Qinglin主席并全国政协各位副主席，<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; Wen Jiabao总理并国务院各位副总理、国务委员：<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 20多年来，特别是近十几年来，大量国有企业和集体所有制单位被私有化，公有资产被侵吞，许多职工下岗、失业，许多农民失去土地。工人阶级和农民兄弟对这种强力推行的私有化，在极其困难的条件下进行抗争，此伏彼起，屡败屡战，从未停止。2009年7月24日爆发的吉林省通化钢铁厂事件，就是这种抗争的一个最新的突出事件。由于广大人民越来越深地尝到私有化的苦果，思想觉悟日益提高，所以这一事件的信息一经传播，迅速引起了各地人民群众和正义人士的强烈反响。他们不仅大力支援通钢工人的正义斗争，而且强烈要求立即制止和纠正一切私有化活动，正在形成一个前所未有的全国范围的反对复辟资本主义的高潮。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 这场斗争是维护还是背离《中华人民共和国宪法》的斗争，是捍卫还是侵占和破坏社会主义国有经济的斗争，是保护还是剥夺工人阶级生存权、劳动权等合法权益的斗争，是坚持马克思列宁主义毛泽东思想、反对修正主义和资产阶级自由化的斗争，是新时期两个阶级、两条路线在经济和政治战线上的斗争。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 《中华人民共和国宪法》规定：“中华人民共和国的社会主义经济制度的基础是生产资料的社会主义公有制，即全民所有制和劳动群众集体所有制。”“国家在社会主义初级阶段，坚持公有制为主体、多种所有制经济共同发展的基本经济制度”（第六条）。“国有经济、即社会主义全民所有制经济，是国民经济中的主导力量。国家保障国有经济的巩固和发展。”（第七条）“社会主义的公共财产神圣不可侵犯。”“国家保护社会主义的公共财产。禁止任何组织或者个人用任何手段侵占或者破坏国家的和集体的财产。”（第十二条）<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 现实情况又是怎样的呢？根据有关材料测算（国家统计局未提供全面材料），2007年我国公有制经济与私有制经济的比重，在实收资本（二、三产业）中，大体是46%比54%；在从业人员（二、三产业）中，是30%比70%；在国内生产总值中，是35%比65%。再从在国民经济中占有重要地位的工业部门来看，据国家统计局材料，2006年国有工业总产值只占全国工业总产值的14.9%，甚至远远低于1949年建国之初国有工业总产值占26.2%的比重。当前公有制经济的比重还在不断地降低。请问，我国宪法庄严规定的社会主义初级阶段基本经济制度还存在吗？<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 宪法规定：“社会主义公有制消灭人剥削人的制度，实行各尽所能，按劳分配的原则。”“国家在社会主义初级阶段……坚持按劳分配为主体、多种分配方式并存的分配制度。”（第六条）。现实情况又是怎样的呢？由于公私经济比重的根本变化，必然造成贫富悬殊、两极分化。国际上通行的反映收入差距的基尼系数，我国在1985年为0.24，1995年上升到0.434，2004年上升到0.469。这不仅超过了公认的0.4的警戒线，而且超过了实行按资分配的法、德、美、日等资本主义国家和印度、印尼、伊朗、埃及等大多数发展中国家。请问，宪法庄严规定的按劳分配为主体还存在吗？<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 宪法规定：“中华人民共和国是工人阶级领导的、以工农联盟为基础的人民民主专政的社会主义国家。”“社会主义制度是中华人民共和国的根本制度。禁止任何组织或者个人破坏社会主义制度”（第一条）“中华人民共和国公民有劳动的权利和义务。”（第四十二条）。现实的情况又是怎样的呢？大量国有企业经过“改制重组”、“小企业卖掉、大企业股份化”、“管理层收购”等等方式被私有化之后，大量工人被迫以“优化组合”、“减员增效”、“内退”、“买断工龄”等等名目下岗、失业，生活陷入困境。在岗的职工也沦为雇佣劳动者（马克思称之为“雇佣奴隶”）。广大工人群众由国家的主人变成弱势群体、贫困群体，政治、经济和社会地位空前降低。请问，宪法庄严规定的工人阶级作为人民民主专政政权领导者的地位还存在吗？工人阶级和劳动人民还是国家的主人吗？<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 总之，宪法规定的我们国家的社会主义经济基础和上层建筑，已经并继续遭受极为严重的破坏。捍卫中国共产党在马克思列宁主义毛泽东思想指导下领导全国人民长期艰苦奋斗，无数革命先烈抛头颅洒热血得来的社会主义制度，捍卫工人阶级用长期艰辛劳动建立起来的国有经济，重建社会主义公有制的主体地位，恢复工人阶级和广大人民的国家主人地位，维护确认和保障上述成果的《中华人民共和国宪法》的尊严和权威，是摆在全国人民面前十分迫切、关乎全局的重大任务。为此，特提出以下十项倡议。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 第一，大力支援通钢工人兄弟的正义斗争，为捍卫和壮大国有制的通钢奋斗到底。日前吉林省政府宣布“终止建龙集团在通钢增资扩股”，只是通钢员工斗争取得的初步胜利。要坚决反对任何资本家在任何时候以“参股”“控股”或其他名义将通钢国企私有化。在今后的斗争中要很好地注意政策和策略。国家应按照宪法加强对通钢的政策和资金支持。全体员工应当更好地团结起来，努力发挥社会主义全民所有制的优越性，进一步把生产搞好。在7月24日的斗争中，由吉林省国资委对通钢进行改制重组后的新总经理陈国君（私资建龙集团人员），视宪法如废纸，视工人如草芥，公然威胁要解雇全体工人，剥夺宪法赋予工人的劳动权（他过去确实有过此类专横行为），以致引发众怒，激化矛盾，酿成被殴不治身亡的事件。这一事件在互联网上传开之后，所有的网友都“一边倒”地站在通钢工人一边；而对于陈国君之死，则无人表示同情，甚至有很多人叫好，可见民心之向背。吉林省国资委的官员竟然指责工人“骚乱”是因为他们具有不该有的“国有情结”（即对社会主义国有经济的衷心爱护），也从反面证明了通钢员工斗争的合法性和正义性。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; Hu Jintao总书记提出“党的事业至上，人民利益至上，宪法和法律至上”。当地政府以“改制重组”为名将国企私有化，是违反宪法和法律的错误行为，应当认真反思，彻底查清推行私有化过程中给国家和员工造成的损失，依法予以补偿，并向全体通钢工人和全社会公开检讨，保证今后严格遵照宪法和法律办事。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 《中华人民共和国刑法》第二条规定：“中华人民共和国刑法的任务，是用刑罚同一切犯罪行为作斗争，以保卫国家安全，保卫人民民主专政的政权和社会主义制度，保护国有财产和劳动群众集体所有的财产，保护公民私人所有的财产，保护公民的人身权利、民主权利和其他权利，维护社会秩序、经济秩序，保障社会主义建设事业的顺利进行。”第十三条规定：“一切危害国家主权、领土完整和安全，分裂国家、颠覆人民民主专政的政权和推翻社会主义制度，破坏社会秩序和经济秩序，侵犯国有财产或者劳动群众集体所有的财产，侵犯公民私人所有的财产，侵犯公民的人身权利、民主权利和其他权利，以及其他危害社会的行为，依照法律应当受刑罚处罚的，都是犯罪。”<br />
