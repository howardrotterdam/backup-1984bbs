关于维护宪法权威、捍卫社会主义<br />
?? 国有经济、重建公有制主体地位的倡议<br />
――从通化钢铁厂事件说起<br />
??<br />
??Hu Jintao总书记并中共中央政治局各位委员，<br />
??Wu Bangguo委员长并全国人大常委各位委员，<br />
??Jia Qinglin主席并全国政协各位副主席，<br />
??Wen Jiabao总理并国务院各位副总理、国务委员：<br />
??<br />
??<br />
??20多年来，特别是近十几年来，大量国有企业和集体所有制单位被私有化，公有资产被侵吞，许多职工下岗、失业，许多农民失去土地。工人阶级和农民兄弟对这种强力推行的私有化，在极其困难的条件下进行抗争，此伏彼起，屡败屡战，从未停止。2009年7月24日爆发的吉林省通化钢铁厂事件，就是这种抗争的一个最新的突出事件。由于广大人民越来越深地尝到私有化的苦果，思想觉悟日益提高，所以这一事件的信息一经传播，迅速引起了各地人民群众和正义人士的强烈反响。他们不仅大力支援通钢工人的正义斗争，而且强烈要求立即制止和纠正一切私有化活动，正在形成一个前所未有的全国范围的反对复辟资本主义的高潮。<br />
??<br />
??这场斗争是维护还是背离《中华人民共和国宪法》的斗争，是捍卫还是侵占和破坏社会主义国有经济的斗争，是保护还是剥夺工人阶级生存权、劳动权等合法权益的斗争，是坚持马克思列宁主义毛泽东思想、反对修正主义和资产阶级自由化的斗争，是新时期两个阶级、两条路线在经济和政治战线上的斗争。<br />
??<br />
??《中华人民共和国宪法》规定：“中华人民共和国的社会主义经济制度的基础是生产资料的社会主义公有制，即全民所有制和劳动群众集体所有制。”“国家在社会主义初级阶段，坚持公有制为主体、多种所有制经济共同发展的基本经济制度”（第六条）。“国有经济、即社会主义全民所有制经济，是国民经济中的主导力量。国家保障国有经济的巩固和发展。”（第七条）“社会主义的公共财产神圣不可侵犯。”“国家保护社会主义的公共财产。禁止任何组织或者个人用任何手段侵占或者破坏国家的和集体的财产。”（第十二条）<br />
??<br />
??现实情况又是怎样的呢？根据有关材料测算（国家统计局未提供全面材料），2007年我国公有制经济与私有制经济的比重，在实收资本（二、三产业）中，大体是46%比54%；在从业人员（二、三产业）中，是30%比70%；在国内生产总值中，是35%比65%。再从在国民经济中占有重要地位的工业部门来看，据国家统计局材料，2006年国有工业总产值只占全国工业总产值的14.9%，甚至远远低于1949年建国之初国有工业总产值占26.2%的比重。当前公有制经济的比重还在不断地降低。请问，我国宪法庄严规定的社会主义初级阶段基本经济制度还存在吗？<br />
??<br />
??宪法规定：“社会主义公有制消灭人剥削人的制度，实行各尽所能，按劳分配的原则。”“国家在社会主义初级阶段……坚持按劳分配为主体、多种分配方式并存的分配制度。”（第六条）。现实情况又是怎样的呢？由于公私经济比重的根本变化，必然造成贫富悬殊、两极分化。国际上通行的反映收入差距的基尼系数，我国在1985年为0.24，1995年上升到0.434，2004年上升到0.469。这不仅超过了公认的0.4的警戒线，而且超过了实行按资分配的法、德、美、日等资本主义国家和印度、印尼、伊朗、埃及等大多数发展中国家。请问，宪法庄严规定的按劳分配为主体还存在吗？<br />
??<br />
??宪法规定：“中华人民共和国是工人阶级领导的、以工农联盟为基础的人民民主专政的社会主义国家。”“社会主义制度是中华人民共和国的根本制度。禁止任何组织或者个人破坏社会主义制度”（第一条）“中华人民共和国公民有劳动的权利和义务。”（第四十二条）。现实的情况又是怎样的呢？大量国有企业经过“改制重组”、“小企业卖掉、大企业股份化”、“管理层收购”等等方式被私有化之后，大量工人被迫以“优化组合”、“减员增效”、“内退”、“买断工龄”等等名目下岗、失业，生活陷入困境。在岗的职工也沦为雇佣劳动者（马克思称之为“雇佣奴隶”）。广大工人群众由国家的主人变成弱势群体、贫困群体，政治、经济和社会地位空前降低。请问，宪法庄严规定的工人阶级作为人民民主专政政权领导者的地位还存在吗？工人阶级和劳动人民还是国家的主人吗？<br />
??<br />
??总之，宪法规定的我们国家的社会主义经济基础和上层建筑，已经并继续遭受极为严重的破坏。捍卫中国共产党在马克思列宁主义毛泽东思想指导下领导全国人民长期艰苦奋斗，无数革命先烈抛头颅洒热血得来的社会主义制度，捍卫工人阶级用长期艰辛劳动建立起来的国有经济，重建社会主义公有制的主体地位，恢复工人阶级和广大人民的国家主人地位，维护确认和保障上述成果的《中华人民共和国宪法》的尊严和权威，是摆在全国人民面前十分迫切、关乎全局的重大任务。为此，特提出以下十项倡议。<br />
??<br />
??第一，大力支援通钢工人兄弟的正义斗争，为捍卫和壮大国有制的通钢奋斗到底。日前吉林省政府宣布“终止建龙集团在通钢增资扩股”，只是通钢员工斗争取得的初步胜利。要坚决反对任何资本家在任何时候以“参股”“控股”或其他名义将通钢国企私有化。在今后的斗争中要很好地注意政策和策略。国家应按照宪法加强对通钢的政策和资金支持。全体员工应当更好地团结起来，努力发挥社会主义全民所有制的优越性，进一步把生产搞好。在7月24日的斗争中，由吉林省国资委对通钢进行改制重组后的新总经理陈国君（私资建龙集团人员），视宪法如废纸，视工人如草芥，公然威胁要解雇全体工人，剥夺宪法赋予工人的劳动权（他过去确实有过此类专横行为），以致引发众怒，激化矛盾，酿成被殴不治身亡的事件。这一事件在互联网上传开之后，所有的网友都“一边倒”地站在通钢工人一边；而对于陈国君之死，则无人表示同情，甚至有很多人叫好，可见民心之向背。吉林省国资委的官员竟然指责工人“骚乱”是因为他们具有不该有的“国有情结”（即对社会主义国有经济的衷心爱护），也从反面证明了通钢员工斗争的合法性和正义性。<br />
??<br />
??Hu Jintao总书记提出“党的事业至上，人民利益至上，宪法和法律至上”。当地政府以“改制重组”为名将国企私有化，是违反宪法和法律的错误行为，应当认真反思，彻底查清推行私有化过程中给国家和员工造成的损失，依法予以补偿，并向全体通钢工人和全社会公开检讨，保证今后严格遵照宪法和法律办事。<br />
??<br />
??《中华人民共和国刑法》第二条规定：“中华人民共和国刑法的任务，是用刑罚同一切犯罪行为作斗争，以保卫国家安全，保卫人民民主专政的政权和社会主义制度，保护国有财产和劳动群众集体所有的财产，保护公民私人所有的财产，保护公民的人身权利、民主权利和其他权利，维护社会秩序、经济秩序，保障社会主义建设事业的顺利进行。”第十三条规定：“一切危害国家主权、领土完整和安全，分裂国家、颠覆人民民主专政的政权和推翻社会主义制度，破坏社会秩序和经济秩序，侵犯国有财产或者劳动群众集体所有的财产，侵犯公民私人所有的财产，侵犯公民的人身权利、民主权利和其他权利，以及其他危害社会的行为，依照法律应当受刑罚处罚的，都是犯罪。”<br />
??<br />
??包括陈国君（2005年私资建龙集团参股后派驻曾任通钢副总经理、2009年建龙控股后任总经理）在内的吉林省国资委、北京建龙重工集团等单位的少数人，执意违反《中华人民共和国企业国有资产法》以下各条（款）。第20条：“ 国家出资企业依照法律规定，通过职工代表大会或者其他形式，实行民主管理。”第37条：“国家出资企业的合并、分立、改制、解散、申请破产等重大事项，应当听取企业工会的意见，并通过职工代表大会或者其他形式听取职工的意见和建议。”第41条第2款：“ 企业改制涉及重新安置企业职工的，还应当制定职工安置方案，并经职工代表大会或者职工大会审议通过。” 第71条 ：“ 国家出资企业的董事、监事、高级管理人员有下列行为之一，造成国有资产损失的，依法承担赔偿责任；属于国家工作人员的，并依法给予处分：（三）在企业改制、财产转让等过程中，违反法律、行政法规和公平交易规则，将企业财产低价转让、低价折股的”；以及第10条、第14条 、第26条、第33条、第68条第三款和第69条的有关规定。按照该法第75条：“违反本法规定，构成犯罪的，依法追究刑事责任。”他们还触犯《中华人民共和国刑法》第397条：“国家机关工作人员滥用职权或者玩忽职守，致使公共财产、国家和人民利益遭受重大损失的，处三年以下有期徒刑或者拘役；情节特别严重的，处三年以上七年以下有期徒刑。本法另有规定的，依照规定。国家机关工作人员徇私舞弊，犯前款罪的，处五年以下有期徒刑或者拘役；情节特别严重的，处五年以上十年以下有期徒刑。本法另有规定的，依照规定。”<br />
??<br />
??面临通钢再次被强行私有化，共和国的财产被大量侵吞、宪法赋予公民的基本权利――劳动权被剥夺的危机关头，通钢广大员工高举“还我国企”、“建龙滚出去”的旗帜集体进行抗争，正符合刑法第二十条 “为了使国家、公共利益、本人或者他人的人身、财产和其他权利免受正在进行的不法侵害，而采取的制止不法侵害的行为……”的规定，是合法的、正义的，完全属于正当防卫行为。刑法第二十条同时规定“对不法侵害人造成损害的，属于正当防卫，不负刑事责任。”实际经验表明，目前在私有化浪潮广为泛滥、某些官员与不法资本家互相勾结，而党政领导又无人站出来维护工人阶级利益的情况下，工人群众被迫只能采取极端的防卫手段，才能阻止国有财产被非法占有的犯罪行为。事件发生后，首先应当依法受到惩罚的，是导致这次事件发生的吉林地方政府中有违法和犯罪行为的直接负责的管理人员和其他直接负责人员以及不法资本家，而不是为维护国家财产和宪法赋予公民的正当权利，为制止犯罪而实施正当防卫的通钢员工。对通钢事件的处理，也是检验我国司法机关是否认真贯彻“三个至上”的一个标志性事件。<br />
??<br />
??第二，一切对国有经济实行私有化的行动，应遵照宪法规定立即停止。经济体制改革本来应当是为了完善和发展社会主义经济，但某些人将经济体制改革篡改为“改制重组”即把公有制改变为私有制，这是违反宪法的，应当立即停止执行。国有企业扩大资金，除自己努力积累外，应遵照宪法规定的原则由财政拨款或由国家银行贷款，而不能依靠私人资本家（他们入股的资金实际上大部分来自国家银行的贷款）。对于现在仍在以“小卖大股”、“增资扩股”等名目，将公有制企业私有化的行为，应支持各地工人群众联合起来，以集体力量坚决地、有秩序地依法制止。<br />
??<br />
??第三，对于过去在国有企业私有化过程中被非法侵吞、贪占的资产，应当彻底揭发，重新收归国有。诸如通过企业“管理层收购”（MBO）将大量国有资产由管理者据为己有；某些官员故意压低国有资产价格，将国企半卖半送给私人；国企管理者通过“主辅分离”将国有的优质资产转入自己参股的辅业，从中渔利；利用亲朋好友成立私营企业，将大量效益很好的业务转包给私营企业，自己从中获利；如此等等，各地政府和社会组织都应当发动广大职工群众举报揭发。国家主管机关应当遵照宪法关于“禁止任何组织或者个人用任何手段侵占或者破坏国家的和集体的财产”的规定，自觉地履行宪法和法律规定的职责，认真查处，决不姑息。<br />
??<br />
??第四，各级人代会、政协、政府部门、监察机关、审计机关要广开言路，主动听取广大群众的意见。主管机关及各种媒体，应在新华网、人民网等全国的以及地方的官方和民间网站上，在《人民日报》、《经济日报》等全国的以及地方的官方和民间报纸上，设置专栏，让群众对侵吞公有财产等违法行为充分揭发并发表意见，使言路畅通，下情上达。同时，允许群众行使宪法规定的游行示威等权利，以表达他们的意见。这是防止官僚主义者和买办势力阻塞言路、激化矛盾、酿成事端的有效方法，是保障真正的而不是表面的社会稳定，实现长治久安的有效方法。<br />
??<br />
??第五，建议党中央、全国人大、全国政协、国务院正视当前公有制经济已失去主体地位的严峻现实，从方针、政策、法律上采取坚决有力的措施，重建公有制经济的主体地位。在社会主义初级阶段，私有制经济具有发展社会生产力的积极方面，可以有一定程度的发展；但它的剥削劳动者和追求剩余价值最大化的本质，与社会主义制度存在着根本矛盾，因此，决不能“喧宾夺主”，让它占据主体地位。在一个国家中，占主体地位的生产关系决定它的社会性质。私有制经济占据主体地位，是与宪法规定的“社会主义制度是中华人民共和国的根本制度”相背离的。因此，建议党中央、国务院责成统计部门和有关部门，查清当前各种所有制经济的状况，按照宪法的要求，制定重建公有制经济的主体地位的规划，并付诸实施，以保证我们的国家沿着社会主义的正确道路向前发展。<br />
??<br />
??建议全国人大遵照宪法的有关规定，特别是关于“社会主义的公共财产神圣不可侵犯”的规定，对现行的一切法律、法规、规章和规定进行审查，凡是与宪法相抵触的，一律加以废止或修订。同时，尽快制定和实施合作社法，以利于鼓励和支持城乡集体经济的发展。<br />
??<br />
??建议国务院在经济体制改革中，首先要优化所有制结构，其内容应当是扩大公有制经济比重，逐步缩小私有制比重，而不是相反。遵照宪法的规定，应立即撤消让私营经济（包括外资）进入应由国家控制的关乎国民生计的重要行业（即西化派攻击的国有经济垄断部门）的法令，同时可在国有重要行业中分设若干国有公司，以引进竞争机制，促进国有经济的蓬勃发展。目前国有工业总产值竟然降到全国工业总产值的14.9%（2006），远低于建国之初1949年的26.2%，已经或正在丧失主导地位，应作为一个重大问题，由国务院对国有资产管理委员会实行问责。在辨明是非、分清责任的基础上，端正指导思想，对国资委进行组织整顿，以确保宪法关于“国家保障国有经济的巩固和发展”这一庄严规定的贯彻落实。<br />
??<br />
??第六，立即停止“引进外资改造国有企业”的错误做法。这种把我国社会主义国有企业变为半殖民地化企业的做法，是违背宪法的规定的。实践证明，“以合资换技术”“以市场换技术”都是失败的。一定要发扬我国的“两弹一星”和“载人航天”的自力更生精神，发挥我国社会主义对核心技术“集体攻关”和“大协作”的优势，独立自主，勇于创新，坚决摆脱对国外的技术依附。近来若干城市为争取世界500强等国外大公司总部进驻而对外资展开“超国民待遇”新一轮恶性竞争的做法，是很不妥当的，应当立即纠正。<br />
??<br />
??第七，现有国有企业应当进行整顿，重新贯彻全心全意依靠工人阶级的方针。在社会主义国有企业中要重新倡导和推行过去行之有效、大放光彩的“鞍钢宪法”、“大庆精神”，充分调动全体职工建设社会主义的积极性。认真实行和完善国有企业的民主管理制度，充分发挥职工代表大会的作用，坚决维护职工的合法权益，提高工人的主人翁自觉性。近年来许多国有企业不同程度地照搬资本主义压榨工人的管理制度和管理方法，是错误的，应当坚决纠正。<br />
??<br />
??第八，鼓励、支持和引导下岗工人和失业工人组织起来，发展城镇集体所有制经济。国家应从贷款、技术培训、物资供应和税负优惠等方面给予大力支持。改变目前各地方、各部门支持私营企业和个体户“创业”和经营，而不支持或很少支持集体经济的错误做法。<br />
??<br />
??第九，大力建设以合作经济（集体经济）为主体的社会主义新农村，从根本上解决“三农问题”。分散的、细碎的家庭经营，不仅使农业无法进行适度规模经营和采用先进技术，而且必然产生两极分化，不可能达到共同富裕。这是产生“三农问题”的症结所在。事实证明，江苏华西村、河南南街村、北京韩村河等几千个一直坚持集体经济的村庄，不仅大幅度提高了生产，而且实现了某种程度的共同富裕，那里没有贫富悬殊，也很少社会丑恶现象。曾以带头包产到户而闻名全国的安徽省小岗村干部亲身参观先进的集体经济组织后，也表示要向这些村庄学习。建议中央和国务院对这些一直坚持集体经济的成功典型，予以大力宣传和表扬。同时，通过典型示范、政府扶持、法律保护等多种举措，在全国农村逐步建立形式多样的各种合作经济组织。各级有关部门和有关的国有企业，也要加强对农村合作社的支援与协作。这是解决“三农问题”唯一正确的道路。那种希望经过土地流转，使土地集中于农业资本家或城市资本家来解决“三农问题”的观点和做法，是与宪法的规定相背离的，也是违背广大农民群众的要求的。<br />
??<br />
??第十，高举马克思列宁主义、毛泽东思想的伟大旗帜，广泛开展学习和落实宪法的活动，以带动各项工作沿着社会主义的道路胜利前进。宪法是一切工作、一切活动必须遵守的规范和准绳。宪法的内容十分广泛和丰富，其核心是根据马克思列宁主义、毛泽东思想，结合我国实际情况，明确地规定了我们这个社会主义国家要有什么样的经济基础、什么样的上层建筑。它的经济基础是社会主义公有制（在现阶段是以公有制为主体），它的上层建筑是工人阶级领导的、工农联盟为基础的人民民主专政的政权和马克思主义的指导思想。由于多年来不学习、不宣传宪法，许多糊涂观念和错误观念不胫而走。修正主义和资产阶级自由化学者则大讲“不问姓社姓资”、“不问姓公姓私”，甚至公开宣传什么“人间正道私有化”等等。最近在通钢事件中，该省国资委的主要官员竟然责备职工群众不该有“国有情结”，这令人怀疑他很可能是一位具有“私有情结”的“爱资病”患者。那位由私资控股的通钢新总经理声色俱厉地狂言要命令全体工人下岗，剥夺他们的劳动权。这说明他们是从来没有学习过（或根本不愿学习）宪法的。在当前情况下，由各级领导干部带头，同广大群众一起，认真学习宪法，才能正本清源，拨乱反正，辨明是非。<br />
??<br />
??学习宪法是为了落实宪法。当前落实宪法首先要敢于正视公有制实际上已丧失主体地位的严重问题，采取切实有力的应对政策措施。更值得注意的是：在近年来私有制经济已占据主体地位的条件下，我国国民经济已经基本上受着资本主义经济规律的支配。资本主义的生产社会化与生产资料私人占有的基本矛盾，导致我国发生了产能和产品过剩的内在危机，生产发展与人民消费能力之间的差距越来越大，其严重程度不亚于某些资本主义国家；只是这种过剩危机被高度的对外依附性掩盖了。这种在表面上掩盖的代价包括：忍受过高的剩余价值率（我国工人只能拿到相当美国工人四十分之一的工资）、过多的工人职业病（最近发生的开胸验肺是一个典型例子）、严重的环境退化和资源耗减、政治上和经济上对外依附度日益加深，等等。我国要走出当前的困境，目前所采取的措施，主要是靠国家财政进行补贴和增加投资以提高购买力，但国家财力是有限度的；银行发放大量贷款，除了存在形成泡沫和通货膨胀的风险之外，扩大基本建设所形成新的生产能力，很可能为今后进一步加大产能和产品过剩、发生更大的危机埋下种子。这些措施可能使GDP在相当时期中有一定的增长，但无法从根本上解决资本主义固有矛盾所产生的消费能力不足和生产过剩的危机问题。<br />
??<br />
??最令人难以理解的是：现在流行着一种观点和做法，即要从日趋没落、危机深重的资本主义制度中寻求中国经济的出路。有的领导机关指示：“进一步优化所有制结构”，就是要“推动非公有制经济和中小企业的发展”。在他们看来，目前我国私有制经济已占全部GDP的三分之二还不够，还要进一步“私进公退”，让私有制经济占有更大的比重。此令一下，宏观管理部门为私有制经济进入应当由国家控制的关乎国民生计的重要行业大开绿灯；银行对私有制企业大量发放贷款；人力资源部门和工会组织积极为私有制企业招工，增加雇佣劳动者，有的还压低工人工资；如此等等。所有这些与我国宪法关于社会主义制度是我国根本制度的庄严规定不是背道而驰吗？如果继续沿着这条道路走下去，我国在资本主义经济固有矛盾的支配下，必然导致国内贫富悬殊的更加扩大、阶级矛盾更加激化、广大劳动人民的消费能力难以提高，同时在对外关系上导致进一步加深对资本主义世界特别是对美帝国主义的依附化（半殖民地化），这就难免有一天会重蹈前苏联国家公开变质和解体的覆辙！<br />
??<br />
??维护宪法权威，维护广大人民群众的根本利益，维护社会主义初级阶段的基本经济制度，是我们党和国家政权各级组织义不容辞的神圣职责。全国的工人农民兄弟、爱国知识分子和一切爱国同胞，要高举马克思列宁主义、毛泽东思想的伟大旗帜，紧密地团结起来，坚决维护宪法的尊严和权威！坚决拥护和支持党和政府落实宪法的各项政策措施！坚决同一切背离宪法的观念和行为进行持久不懈的斗争！要用维护和落实宪法的实际行动迎接新中国建立六十周年华诞！要努力争取我国社会主义现代化建设事业获得新的伟大胜利！<br />
