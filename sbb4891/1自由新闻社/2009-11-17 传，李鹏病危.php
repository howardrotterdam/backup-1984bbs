																											<div id="postmessage_330371" class="t_msgfont">李鹏病重，中办准备后事<br />
发布: 2009-11-17 03:20 <br />
明镜网特约记者黎佳<br />
<br />
北京政治观察人士对明镜网说，81岁的中国前国务院总理李鹏再传病重，中共中央办公厅已有人专责准备处理李鹏的后事。李鹏一旦去世，为更加和缓处理六四后遗症扫除了一大障碍。<br />
<br />
对李鹏在六四事件中扮演的角色，香港出版的《北京政人物之元老干政》有详细披露。该书指李鹏是“六四北京政变的主谋和总导演”。《北京政人物之元老干政》作者刘京威对明镜网说，李鹏一直是反对六四事件平反的最强硬元老。虽然他并没有在六四事件后升为中共中央总书记，但六四事件将他推到了一个极端位置，几乎是“你死我活”状态。<br />
 <br />
在最近中共元老钱学森和谷牧遗体告别仪式上，胡锦涛、江泽民等多位现、前国家领导人均出席，但前总理李鹏均没有露面，再次引发他病重无法出席的传闻和猜测。<br />
<br />
 <br />
新华社的报道称，在钱学森和谷牧病重期间和逝世后，胡锦涛、江泽民、吴邦国、温家宝、贾庆林、李长春、习近平、李克强、贺国强、周永康等，前往医院看望或通过各种形式对钱学森的逝世表示沉痛哀悼并向其亲属表示深切慰问。<br />
<br />
 李鹏亦列在“病重期间和逝世后，前往医院看望或以各种形式向其亲属表示慰问的”名单中。但北京政治观察人士对明镜网说，“以各种形式”包涵的范围太广了，并不等于李鹏可以亲为亲为了。<br />
<br />
 出席钱学森告别仪式的有：<br />
<br />
 胡锦涛、江泽民、吴邦国、温家宝、贾庆林、李长春、习近平、李克强、贺国强、周永康、王刚、王岐山、回良玉、刘淇、刘延东、李源潮、张德江、郭伯雄、朱F基、李瑞环、宋平、尉健行、李岚清、曾庆红、令计划、陈至立、李建国、陈昌智、梁光烈、廖晖、杜青林、白立忱、陈奎元、黄孟复、张梅颖、张榕明、钱运录、李金华、郑万通、万钢、罗富和和杨白冰、田纪云、迟浩田、张万年、吴仪、曹刚川、曾培炎、邹家华、布赫、彭佩云、李铁映、许嘉璐、蒋正华、顾秀莲、热地、肖扬、贾春旺、杨汝岱、宋健、钱正英、孙孚凌、王忠禹、李贵鲜、罗豪才、郝建秀、徐匡迪、李蒙，中央军委委员陈炳德、李继耐、廖锡龙、常万全、靖志远，以及傅全有、于永波、王瑞林等。<br />
<br />
 出席谷牧告别仪式的有：<br />
<br />
 胡锦涛、江泽民、吴邦国、温家宝、贾庆林、李长春、李克强、贺国强、周永康、王刚、王兆国、王岐山、刘淇、刘延东、李源潮、张德江、郭伯雄、朱F基、宋平、尉健行、李岚清、曾庆红、罗干、何勇、令计划、路甬祥、韩启德、陈至立、李建国、马凯、戴秉国、曹建明、廖晖、杜青林、阿不来提・阿不都热西提、黄孟复、张榕明、钱运录、孙家正、李金华、郑万通、邓朴方、万钢、林文漪、罗富和和吴仪、曾培炎、王汉斌、邹家华、彭云、曹志、何鲁丽、蒋正华、顾秀莲、热地、肖扬、贾春旺、宋健、钱正英、孙孚凌、王忠禹、李贵鲜、罗豪才、徐匡迪、张怀西、李蒙,以及傅全有等。<br />
<br />
 其实，这已经不是第一次传出李鹏病重的消息了，早在2008年前后，海外媒体就称，李鹏身体不佳，情况不容乐观。该报道称，继早前没有出席奥运会开幕式后，李鹏再次缺席华国锋追悼会，引起国外媒体关注。据了解，李鹏从2008年年初中风以来，身体一直严重，长期在北京301医院领导人病房治疗，还更出现面瘫麻木、失禁等情况。<br />
<br />
 关于李鹏的病，媒体曾传出两种不同的版本：一是说李鹏于2008年2月5日中风，送医院抢救已脱离生命危险，但留下了后遗症，“口眼已经歪斜”；一是说李鹏在2008年元旦脑血栓发作，经抢救病情已基本稳定。但对于这两种版本，李鹏之女李小琳一概否认。<br />
<br />
 早在2008年3月的两会期间，首次出任全国政协委员的李小琳，被媒体追问，缺席两会的李鹏是否患重病传闻是否属实，李小琳矢口否认，她说：“老人家有点毛病很正常，就像大家会伤风感冒一样。”但多个媒体的记者注意到，说到这里时，一直很能保持平静的李小琳情绪突然失控，眼泪夺眶而出。<br />
<br />
<br />
《北京政人物之元老干政》作者刘京威对明镜网说，这是一个很有意思的重要细节，儿女对亲生父亲一般是有真感情的，特别是女儿对父亲，如果李鹏的身体真的仅有小毛病，做女儿的怎么会忽然悲从中来，凄然泪下？出面否认李鹏生病可以由“党组织”安排，但在否认父亲患病的时候，一个人的眼泪是不可能撒谎的，如果李鹏身体没问题，其女儿的自然表情应该是愉快或轻松的，显然，李小琳夺眶而出的泪水就是李鹏患病的真实告白，而且流露了李鹏病得不轻。<br />
<br />
<br />
李鹏的衰病，对其渐趋没落的腐败家族无疑是雪上加霜。李鹏的权术手段，对付较为君子做派的赵紫阳还可以，对付权谋大师江泽民则处处捉襟见肘，因此在邓小平死后，江泽民大权独揽，呼风唤雨，而在中央比老江资格更老，六四“平暴”功劳更大，正宗“太子党”出身的李鹏总理，却反而退居人大委员长，其权势仅够自保。十六大退休后，相较江泽民，李鹏权势衰退得厉害，最近几年，中共中央越来越不买他的帐：<br />
<br />
<br />
首先是三峡工程日益遭受冷落。三峡工程是李鹏任总理期间，动用专制权力强行上马的巨型工程，1997年11月6日三峡工程在大坝截流时，江泽民和部分常委亲自捧场，鞭炮，礼炮齐鸣，信号弹升空，五彩缤纷的气球飞舞，媒体铺天盖地一边倒地发出歌颂之词，此后，江泽民也不止一次亲赴视察，大力捧场，可是在 2006年5月20日三峡大坝封顶时，中共国最高领导层出人意料地整体缺席封顶的庆贺典礼，当年截流时的风光荡然无存。<br />
<br />
<br />
近年来，在中共中央默许下，掩盖已久的三峡工程的质量问题，移民安置的腐败问题接连被官员披露。对李鹏更为直接的挑战是：在两会上，10多名由水利，环保专家组成的人大代表提案小组向政府提交了关于三峡工程的提案，要求中央政府对三峡工程生态与社会后果进行检讨，其问责的矛头直指李鹏。<br />
<br />
<br />
对于李鹏动用权力营造的家族电力腐败王国，现今中共当权派也越来越不客气：<br />
