																											<div id="postmessage_311066" class="t_msgfont">中新社成都十一月一日电 (吴志维杨杰)七位全国权威文物评定专家一日在四川建川博物馆评定出三十件一级国家文物，除二十件抗战文物外，邱光华《飞行记录》本等十件与汶川大地震相关的文物，跻身进入国家一级文物行列。<br />
<br />
邱光华在去年的抗震救灾中因直升机失事而以身殉职。中央军委主席胡锦涛签署通令给邱光华追记一等功。邱光华的《飞行记录》是建川博物馆于去年九月在失事现场寻找到的，因被火烧和雨水浸泡，字迹模糊不清，“邱光华”三个字依稀可见。<br />
