													<h2>影帝：大学须具有独立思考自由表达的灵魂</h2>
						
						
																											<div id="postmessage_462490" class="t_msgfont">温家宝：大学须具有独立思考自由表达的灵魂<br />
<a href="http://www.sina.com.cn" target="_blank">http://www.sina.com.cn</a>&nbsp;&nbsp;2010年02月02日00:27&nbsp;&nbsp;新华网<br />
1月26日，温家宝听取科技、教育、卫生、文化、体育界代表对《政府工作报告》的意见。<br />
<br />
　　新华网北京2月1日电 记者 赵承<br />
<br />
　　1月26日上午还不到9时，温家宝总理提前来到了国务院第一会议室，与来自科教文卫体各界的10位代表围坐在椭圆形的桌子旁，听取他们对《政府工作报告(征求意见稿)》的 视频：温家宝召开座谈会征求政府工作报告意见 来源：CCTV《新闻联播》 意见、建议。<br />
<br />
　　“我很愿意和来自科教文卫体各界的人士座谈。”刚一落座，温家宝就亲切地对大家说，“听听大家对《政府工作报告(征求意见稿)》以及对政府工作的意见，听听大家对社会事业发展和改革的意见。”事实上，他就任总理以来在社会事业发展方面倾注了大量的心血，一直致力于改变经济社会发展一条腿长、一条腿短的问题。<br />
<br />
　　10位与会代表中有科学家、教师、医生、教练员、导演等，尽管职业不同，但他们在阅读过《政府工作报告(征求意见稿)》后都有共同的感受，就是报告中关于发展社会事业的篇幅比往年又多了，而且内容更丰富，措施更具体。<br />
<br />
　　“一个国家的实力、一个民族的荣誉，不仅反映在经济实力上，而且反映在社会进步、人的素质、科技水平、文化底蕴和道德力量上。就经济本身而言，没有科技支撑，人的素质的提高，经济就不可能有跨越和可持续的发展。转变发展方式的关键，就是要把经济发展转移到依靠科技进步和提高劳动者素质的轨道上来。”温家宝简练而意味深长的开场白，让大家感受到他对社会事业发展的重视和深思，也激发了大家与总理交流的迫切愿望。<br />
<br />
　　从事结构生物学研究的施一公教授于2008年辞去美国普林斯顿大学终身讲席教授职位，回到清华大学任教，如今是清华大学生命科学院院长。施一公原准备了一个发言稿，听到总理和蔼真诚的这番话后，他放下发言稿，面对总理侃侃而谈。<br />
<br />
　　“您在多个场合表达了对中国大师级人才求贤若渴的心情。但是没有世界一流的大学，就不可能培养世界一流的人才。”施一公开门见山：“温总理，加快世界一流大学建设，应该写进《政府工作报告》中。”<br />
<br />
　　从英国、美国到日本，施一公以史料论证了世界一流大学的建设对于民族昌盛、国家崛起的意义，并指出当前中国大学发展中存在的一些急功近利的现象。<br />
<br />
　　“一些大学功利化，什么都和钱挂钩？这是个要命的问题。”温家宝听后深有感触地说。<br />
<br />
　　施一公向总理列举了世界一流大学的各种标准。他特别强调办学自主权的问题。施一公感慨道：“在大学这个本应该思想最活跃，最富有创造力的地方，如今的教育在管理上‘一刀切’，严重阻碍了学生创造力的培养。”<br />
<br />
　　听了施一公的话，温家宝说出了自己的思考：“一所好的大学，在于有自己独特的灵魂，这就是独立的思考、自由的表达。千人一面、千篇一律，不可能出世界一流大学。大学必须有办学自主权。”<br />
<br />
　　当施一公结束发言时，温家宝对大家说：“我们要下决心加快建设世界一流大学。”他略微停顿一下，提高了声音，语气坚定地说：“中国一定能够出现世界一流大学。”深受感染的施一公也回应道：“一定能。”<br />
<br />
　　中国国家话剧院院长周志强，对下乡演出的场景记忆犹新。他对总理说：“每当下乡演出，看到乡亲们迎着寒风瑟瑟发抖，还站在村边场院上津津有味地看戏时，我们心里特别难受。农民们也应该享受到和城镇居民同等的文化服务。”他还谈到现在一些演出单位过分强调经济利益，过度商业化的问题。<br />
<br />
　　“你讲的是实情。”温家宝接过话头说：“文化教育卫生事业的发展，基本取向应该是公益性的，决不能‘一风吹’地搞产业化。即使是文化产业也应该面向群众，把社会效益放在第一位。”<br />
<br />
　　中国艺术研究院研究员刘梦溪从事文化史和思想史的研究。他认为社会的问题在经济，经济的问题在文化，文化的问题在教育。经济强国的建立，不能以牺牲文化的基本价值为条件。<br />
<br />
　　从中华文化的原典到特性，从“求同存异”到“和而不同”，刘梦溪娓娓而谈。他还提出了建立人文与社会科学的荣誉制度，加大文化领域投入的建议。<br />
<br />
　　听完刘梦溪的发言，温家宝幽默地说：“刘老师给我们上了一堂文化课。”在座的人都开心地笑了起来。<br />
<br />
　　温家宝接着谈起自己对儒家文化的理解：儒学一是仁或者爱，“仁者爱人”；二是善，“无恻隐之心非人也。”这是道德的基础；三是和，“和为贵”、 “和而不同”。四是刚健自强，“天行健，君子以自强不息”；五是民本思想，“民为贵，社稷次之，君为轻。”……他扳着指头缓缓地说，话语里浸透着对中华文化的深厚感情和深刻理解。温家宝尤其强调了道德问题。他说，国际金融危机的爆发有各种原因，但不能否认其中一个原因是一些企业家道德沦丧、重利轻义，将经济灾难转嫁到人民身上。<br />
<br />
　　“你这个科室比较特殊呀！”当来自北京协和医院的尹佳介绍自己是变态反应科主任时，温家宝诙谐地说道。这引得大家笑了起来。尹佳解释道，变态反应其实就是过敏性反应。随后，尹佳向总理具体介绍了他们科室和医院的情况。<br />
<br />
　　尹佳讲了身为医生的难处和委屈：变态反应科有9个人，每年要接诊7万个病人，还要搞18万人的流行病调查，非常辛苦。但是，医生的劳动价值并没有得到真正的体现。她身边一些年纪比较大的医生患上大病时，也同样面临着看不起病的问题。<br />
<br />
　　听了尹佳的话，温家宝思考后说：“公立医院改革是医药卫生体制改革的难点。通过改革既要让患者满意，又要调动广大医护人员的积极性。这一步不好迈，但我们有决心迈过去。”<br />
<br />
　　由于代表人数比较多、时间有限，每个代表规定发言12分钟。但是，每个人都超过了规定的时间。大家没有客套，都挑最实在、最要紧的话向总理讲，会场内自始至终充满着坦诚交流的气氛。<br />
<br />
　　江苏省扬州市梅岭中学校长王力耕谈了中小学生素质教育问题，中国科学院化学研究所研究员徐坚建议注重对国内战略性新兴产业的政策保护，河南省邓州市中医院院长唐祖宣希望推动中医药更好地服务农民，中国电影集团艺创中心导演胡玫谈了中国电影应提高质量并走出去，高等教育出版社社长李朋义建议加快出版业的规模化集约化，国家乒乓球队男队主教练刘国梁提出进一步重视运动员文化教育和保障工作……<br />
