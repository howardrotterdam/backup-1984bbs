<br />
华商日报3月12日报道 中国人口应控制在15亿左右，先控然后递减到大约5亿左右，有助于人均国力强大。<br />
<br />
　　昨日，本报和某网站联合采访全国人大代表，中国社科院学部委员、马克思主义研究院院长，全球学术团体―――世界政治经济学学会会长程恩富，就时下热点话题展开讨论。中国人口减到五亿，您赞成OR反对？&gt;&gt;&gt;我要PK<br />
<br />
　　中国本世纪末进入发达国家的可能性只有5%<br />
<br />
　　记者：最近社会上有舆论希望调整计划生育政策，甚至想放开二胎，您同意这个观点吗？<br />
<br />
　　程恩富：计划生育30年少生了4亿多人，我们每一个人，包括年轻人，都是享受了少生4亿人的好处。<br />
<br />
　　我认为，现有传统资源的利用已经到了极限，我们应该看到我国的人口基数很大，每年新增人口数量大（去年新增人口就达670万人）。<br />
<br />
　　所以，最近中国科学院的一个中国现代化的研究报告里就指出，中国在本世纪末，要进入发达国家的可能性只有5%。<br />
<br />
　　很多人看到这个数字很吃惊，我们好像进入发达国家机会不大，实际上不是GDP总量不大，而是人均国力，人均资源占有量，到本世纪末也很难达到发达国家的生活水平。<br />
<br />
　　我主张中国应当是先把人口控制在15亿左右，先控然后再通过严格的计划生育政策，较快地递减人口，递减到大约5亿左右。那么才能在一百年内赶上发达国家。<br />
<br />
记者：您的观点是先控后减吗？<br />
<br />
　　程恩富：用人口密度说，中国这么大块地方，甭说四五亿人，承载20亿人也没问题，日本一亿多人，GDP总量大，实际上它是利用了别国的资源。<br />
<br />
　　现在问题转过来了，如果我们中国15亿人来利用世界各国的资源，会导致一个什么样的结果？我认为这不仅是经济问题，它还是国际政治问题。所以如果我们继续增加人口，而又要保持较高的GDP的增长速度和人均的生活水平的提高，那就必然要以全世界资源的大量获取为前提，这样将对世界的政治经济产生重大影响。<br />
<br />
　　美国开发商利润只有15%，咱们是150%<br />
<br />
　　记者：住房和城乡建设部副部长郭允冲表示，房价调控需要多个部门协助努力，住建部在房价调控几乎没有什么手段，很多人听了比较难过，政府真的没有调控手段了吗？<br />
<br />
　　程恩富：目前两件事可以做的，一、把廉租房、经济适用房和改善房综合起来，可以叫做公租房，中等收入家庭可以住，低收入的人和农民工也可以住。<br />
<br />
　　二、对于开发商捂盘惜售，维持高房价，政府可以对已经建完验收的房子限期销售，开发商到期销售不完就必须要降价，这样可以控制房地产商的暴利。<br />
<br />
　　有消息称，美国房地产商利润大概只有15%左右，而我国开发商的利润可能已经达到了150%。</div>

							
							
							
							
