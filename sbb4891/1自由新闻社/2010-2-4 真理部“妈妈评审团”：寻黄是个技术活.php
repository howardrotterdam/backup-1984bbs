													<h2>真理部“妈妈评审团”召开首次评审会</h2>
						
						
																											<div id="postmessage_465806" class="t_msgfont"><a href="http://net.china.com.cn/txt/2010-01/20/content_3351178.htm" target="_blank">http://net.china.com.cn/txt/2010-01/20/content_3351178.htm</a><br />
<br />
千龙网北京1月19日讯 （记者 安力）今天下午，“知心姐姐”卢勤、江西省的“网络妈妈”刘焕荣、全国劳模李素丽、青少年法律与心理咨询中心主任宗春山等九位青少年教育专家及社会爱心人士被北京网络媒体协会正式聘请为“妈妈评审团”首批评审员，并召开了第一次评审会。<br />
<br />
&nbsp; &nbsp; 近期，青少年教育专家宗春山率先提出了组建“妈妈评审团”的建议，本着“儿童最大利益优先”的原则，从家长的角度对网上不良信息展开监看、评议。这一提议得到了北京网络媒体协会等相关部门的大力支持，立即着手制订评审团章程并开始在网上招募成员。<br />
<br />
&nbsp; &nbsp; 按照“妈妈评审团”的章程，评审团成员的职业要兼顾各行业各阶层，如法律工作者、教育工作者、新闻媒体代表、流动务工人员、专职主妇等，首批成员从自愿报名者中遴选50-100名，其中中小学学生家长比例不少于70%。<br />
<br />
&nbsp; &nbsp; 记者注意到，今天受聘的9位评审员，既有象宗春山、卢勤一样来自法律、教育领域的工作者，也有李素丽这样的具有社会知名度的人士，还有刘焕荣这样的普通母亲。<br />
<br />
 刘焕荣是9位评审团成员中最为特殊的一位，这位身体残疾的母亲来自江西省弋阳县，从2003年开始，她用仅存一个拇指的双手夹笔代指敲击键盘，利用QQ、EMAIL、发帖等方式与青少年及其家长们沟通，帮助他们解除网瘾，成为一名“网络妈妈”。今天，她通过网络视频连线参会，并接受了“妈妈评审团”的邀请，成为评审团的一员，她欣喜地表示，要继续尽自己所能，监督网上的一切有害信息。<br />
<br />
&nbsp; &nbsp; 在今天的第一次评审会上，与会人员共同听取了搜狐焦点网总经理曾伏虎对近期整治网上低俗信息情况的汇报。据其介绍，从2009年12月9日整治活动开展以来，搜狐焦点网已永久关闭了27个涉及低俗内容的高危论坛，删除内容低俗图片160万张、视频571个，设置人工关键词搜索删除了800余条低俗信息。评审团对此给予了一致肯定。<br />
<br />
&nbsp; &nbsp; 据北京网络媒体协会相关负责人介绍，“妈妈”评审员并非一个荣誉称号，她不仅要热心公益事业，还要有很强的社会责任感及清晰的分析判断能力，能够积极参与评审团的活动。同时，评审团成员对评审对象有处置建议权，形成一致意见后提交给相关部门处理，并有权要求相关部门反馈处置结果。<br />
<br />
&nbsp; &nbsp; 记者了解到，“妈妈评审团”从上周起已开始面向社会公开招聘，短短几天就接到了数位热心人士的报名申请，网络媒体协会将认真遴选录用。招聘详情请登录北京网络媒体协会网站（<a href="http://www.baom.org.cn" target="_blank">http://www.baom.org.cn</a> ）<br />
<br />
&nbsp; &nbsp; 北京市公安局、北京市文化局、北京市广播电影电视局、北京市新闻出版局和北京市互联网宣传管理办公室的相关负责同志参会。<br />
<br />
&nbsp; &nbsp; 现场：“妈妈”各抒已见谈扫黄<br />
<br />
&nbsp; &nbsp; 当天，评审团成员对成立“妈妈评审团”发表了各自的观点。<br />
<br />
&nbsp; &nbsp; 虽然不是“妈妈”，但作为“妈妈评审团”的倡导者，宗春山对当选评审团成员感到“很有成就感”，他认为，在成人主导的世界里，儿童是没有能力倡导自己的权利的。在法律尚不够健全的情况下，象“妈妈评审团”这样的民间力量参与到网络扫黄中，将是对儿童利益的最大的保护。<br />
<br />
&nbsp; &nbsp; 卢勤提出，互联网应建设成“护联网”，一方面由爱孩子的父母组织起来过滤、删除掉不健康的网页，另一方面要增强孩子的自我保护能力，对他们进行媒介素质教育，培养他们正确识别、使用、传播媒介的能力。<br />
<br />
&nbsp; &nbsp; 李素丽指出，互联网行业从业人员在上岗前应进行职业道德操守方面的培训，政府相关管理部门要加大对互联网行业的管理。<br />
<br />
&nbsp; &nbsp; 来自中国政法大学青少年犯罪教研室的皮艺军教授则认为，针对当前严打的互联网色情内容而言，暴力内容的危害性更大，对于此类有害信息，应在删除、封堵的同时，加强对青少年的正确引导。他还提出，可以让法律专业的在校大学生担当志愿者，帮助筛查网上不良信息。<br />
<br />
&nbsp; &nbsp; 2010-01-19&nbsp; &nbsp;&nbsp;&nbsp;来源：千龙网</div>

							
							
							
							
