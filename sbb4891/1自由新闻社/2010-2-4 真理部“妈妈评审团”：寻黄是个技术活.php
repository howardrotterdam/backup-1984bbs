“妈妈评审团将参考这一体系的运作模式。”王倩说。<br />
<br />
宗春山认为，“妈妈评审团”之所以如此快速地成立，也和去年开始的全国打击整治网络淫秽色情专项行动有关。<br />
<br />
北京网络媒体协会的业务主管单位是，中共北京市委宣传部和北京市人民政府新闻办公室，虽然是非营利性社会团体法人，但承担着诸多官方职能。<br />
<br />
宗春山承认，这会让“妈妈评审团”这一民间团体带有一定的“政府色彩”。<br />
<br />
网络能否分级制?<br />
<br />
专家宗春山认为黄色网站无法完全杜绝，若实行网络分级制再加强民间监督，作用会更好<br />
<br />
刘焕荣曾指导过许多“深陷网络黄色信息不能自拔”的青少年。这次她也应邀参加了“妈妈评审团”。<br />
<br />
在她看来青春发育期的孩子好奇心强，自制力又弱，如果不予引导教育，很容易出问题。<br />
<br />
在1月29日，妈妈们追问宗春山还有没有更好的方式，让孩子不要沉迷网络，消除黄色信息的技术手段。<br />
<br />
宗春山给出了另一个解决思路。“孩子沉迷网络，黄色信息只是辅因，根本原因是家庭教育出了问题。如果父母不从源头找解决方法，只是想通过禁止孩子上网，消除一切黄色信息来解决问题，这是不现实的。”<br />
<br />
宗春山说，他坚决反对网络是儿童问题的罪魁祸首。<br />
<br />
与目前网络扫黄走红的卫道士不同，宗春山对诸多网络涉黄现象表示出了相当的宽容。宗春山认为，“(黄色内容)是客观存在，仅靠一种强制的方式是无法杜绝的。而且因为有客观需要，所以没有必要完全杜绝。”<br />
<br />
但他强调，必须保证儿童接触的信息是纯净的。<br />
<br />
宗春山了解很多国外保护青少年的做法。<br />
<br />
在美国，有《未成年人互联网保护法》，规定中小学校、公共图书馆等必须在其网络服务程序的目录上提供过滤器，确保未达17岁的未成年人接触不到色情内容。政府还对学校、公共图书馆建立网络过滤技术系统提供资金支持，网络技术服务商在给学校和图书馆提供过滤技术服务时要给予打折优惠。<br />
<br />
任何因商业目的在互联网交流中导致未成年人接触到有害信息的行为，视情节轻重，可以受到不高于5万美元的罚款或被判不长于6个月的刑期的处罚，或者两罚并用。<br />
<br />
在韩国，实行的是实名上网制度，根据身份证信息，网站可以控制未满年龄的青少年不得浏览某些内容。<br />
<br />
宗春山认为，我们可以借鉴外国电影的分级制度，对网络实行分级制。<br />
<br />
“我们不能说政府关闭某些网站是打着保护儿童的幌子，但必须明确的一点，将所有问题归咎于网络黄色信息，无论对于家长还是政府来说，这都是逃避和借口。”宗春山说。<br />
<br />
妈妈们在经过培训后已经显示出一种热情。<br />
<br />
魏琛琛说：“等年后，我会每天抽出一两个小时专门上网查寻涉黄的信息，并会找到一些这方面图书好好学习，最终胜任自己的工作。”<br />
