<br />
 只是问问而已<br />
崔卫平 @ 2009-12-29 12:42 阅读(2185) 评论(24) 引用通告 分类: 未归类<br />
<br />
询问知识分子同行对于刘晓波被重判十一年的做法，绝不是“拷问”知识分子的良心，言“拷问”太重了。只是想知道他们的看法而已，想知道人们知道这件事情之后怎么想的，而一般人们也没有渠道公开这些想法。在我们的公共生活与私人生活之间，存在着巨大的鸿沟。<br />
<br />
而且我知道这样做，自己是有“原罪”的。将人们从自己原本的轨道中拉出来，离开日常生活的光线，来面对这么一个晦涩的问题，在我本人也是下了很大决心的，心里很有些过不去。我平时决不做这种事情。但有的时候，生活的链条断裂了，比如晓波被判重刑，这么大的事情发生在我们中间，我们不得不背负起这个断裂，也背负起自己的“原罪”。 <br />
<br />
这个做法，也是继承先贤孔子的传统。孔子曰：诗可以“兴、观、群、怨”，其中的“观”，就是让人们可以从诗中了解社会生活、政治风俗的盛衰得失，于是这位老先生将三百篇诗歌放在了一起。我将这许多知识分子的看法放在一起，也是提供一个可以“观”的视野。 <br />
<br />
