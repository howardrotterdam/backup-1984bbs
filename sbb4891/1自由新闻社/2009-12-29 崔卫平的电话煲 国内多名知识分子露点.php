作者：石扉客 提交日期：2009-12-29 0:05:00 正常 | 分类:未分类 | 访问量：338<br />
<a href="http://blog.tianya.cn/blogger/post_show.asp?BlogID=33963&amp;PostID=21159414&amp;idWriter=218419&amp;Key=963422520#Bottom" target="_blank">http://blog.tianya.cn/blogger/po ... ey=963422520#Bottom</a><br />
　　<br />
石按：以下是北京电影学院教授崔卫平的电话采访推特记录整理，她在做的，实际上是一个非常重要的知识界动员工作，更是在传播学意义上的一个创举。<br />
　　<br />
另，崔的电话采访都已经过事主授权公开。这个创举还在继续中，用她的话说，那就是“从明天起，谁接到我的电话，心里也许就会说‘鬼来了’”。童庆炳之前（含童）是1225宣判前的采访，之后是宣判后。为规避传播风险，我已将里面的部分敏感词做了处理，最敏感的三个字人名都置换为一个字：“他”。<br />
　　<br />
以下记录，请尽快转走：<br />
<br />
崔卫平的电话煲<br />
　　<br />
诗人芒克称自己与他“是非常好的朋友，在一起都挺愉快的。从来也不认为他做错什么事情。”在听说了他的近况后，芒克说“但愿他明天没有什么事。”<br />
　　<br />
他八十年代的朋友、诗歌评论家唐晓渡回忆，当年黑马他明明认识刘再复，当面却假装说不认识。但他同时说，对于他近年来的思想发展，越来越认同，觉得他越来越结实。在中国目前语境中，他表现出了知识分子的担当和理性。<br />
　　<br />
原北师大教授、鲁迅专家王富仁教授今天仍视他为“朋友”，尽管许多观点不相同。王教授听说他近况之后，说：“人在思想上应该是有自由的，在行动上应该是有限制的。思想自由是不能干涉的。在这个问题上，中国一直没有解决。”<br />
　　<br />
他念博士的导师之一、北师大教授童庆炳先生，听说他近况之后，想对他说：希望他“珍重”。“毕竟有过一段师生情谊，希望他珍重。”<br />
<br />
――20091225――　<br />
　<br />
钱理群先生刚才在电话里说，比起八十年代，他写的文章越来越理性了。他对他印象最深的是两件事：一是五三五的表现，二是坚守国内。钱先生“不觉得他有什么罪。”不一定同意他的所有观点和做法，这是另一回事；但他属于平和、理性的批评者，对他都要判刑，是一种虚弱的表现，觉得很难接受。<br />
　　<br />
汪晖先生来信：谢谢来信和电话。我不赞成他的许多观点，但反对任何以言治罪的方式。我会关注此事的发展，在了解更多的情况之后，表达我的看法。<br />
　　<br />
<strong>作家莫言同意将他的这句话放在这里：不太了解情况，不想谈。家里有客人，正在和他们说话。</strong><br />
　　<br />
丁东的看法：文章自古多奇狱，又添十一载；思想从来要自由，何止零八年。――圣诞中国<br />
　　<br />
乐黛云的看法：一，看过零八县长，觉得其中没有颠覆国家的想法，是希望改善；二、零八县长是一个讨论，如果一个讨论也要定罪，是违背宪法的，不能让人心服口服；三、如果（弄得）大家都不敢说话，国家还怎么改善？<br />
　　<br />
章诒和的看法：1968年，我以现行反革命罪，判处有期徒刑20年；2009年，他以煽动颠覆国家政权罪，判处有期徒刑11年。我们都是因言获罪，前后相距四十一载。这种状况不得不使人怀疑：我们的制度，到底改善了多少？我们的社会，究竟进步了没有？<br />
　　<br />
袁伟时的看法：二十一世纪了，还以言获罪，侵犯公民权利，亵渎文明，又一次往中国脸上擦黑！当局认定的罪犯他成了众人心目中的英雄，鸿沟如此巨大，执政者如何面对？<br />
　　<br />
徐贲的看法：一个中国公民以宪法给他的权利，善意地对国家政治表达自己的看法，却受到如此严厉的惩罚，感到难以置信。中国是一个签署了国际人权公约的国家，中国要在国际上崛起，必须建立良好的言行一致的信誉。从许多国家对此事的报道来看，这是一件使中国信誉受到损害的事情。<br />
　　<br />
秦晖的看法：现在还搞因言治罪那一套，真是太可悲了。我不是县长的签名者，但我懂得“不赞成你的观点，但坚决捍卫你发表观点的权利”的道理，我坚决反对对他搞文字狱。<br />
　　<br />
徐友渔的看法：宣判他，罪名中有08县长。县长重申联合国人权宣言，因此宣判是对人类大家庭公认的文明准则的挑战，也是对中国现存宪法的挑战，因为该宪法载明了中国公民的言论自由。说到底，是对中国人民和人类良知的挑战。<br />
<br />
胡杰的看法：社会已经如此开放，言论自由已经成为全社会的共识，从（对待）谭作人到他，怎么还会出现如此荒唐、倒退的现象？</div>

							
							
							
							
