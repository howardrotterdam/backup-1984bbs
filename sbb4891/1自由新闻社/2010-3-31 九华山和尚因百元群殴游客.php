<br />
<br />
　　九华山和尚和游客打架被网友全程记录<br />
<br />
<br />
　　吃斋念佛的和尚也会动手动脚打群架，这起离谱的事情发生在安徽的九华山。据了解，打斗视频长达3分18秒，上传于3月27日，视频中拍摄者称最初是一名穿着白衬衫蓝马甲的“和尚”手持木棍先动手，追着游客打，从庙里面打出来，后来言语冲突升级，所有目光都集中过来，其中一个穿着橘黄色衣服的和尚跑上去上前一脚踹倒被打游客，接着三四名和尚一起上前围殴，旁人试图劝架，没想到场面还更加失控，连法器都成了武器，在手中的武器被人抢走后，马上回头要找别的东西，一时间找不到，干脆直接动手动脚，最后被其他游客劝阻制止。虽然打架原因不是很清楚，但有游客认为佛门圣地出家人怎么能动手打人呢？视频被拍摄者上传网络后，大多数网友表示愤慨，开始在网络上疯狂转载，引起热议，短短3天内播放次数超过80万次。<br />
<br />
<br />
　　打架事由说法不一 记者证实确有其事<br />
<br />
<br />
　　记者从网上了解到，关于打架的说法不一，没有明确的原因，有网友自称是当时的目击者，称当时和尚和游客打架是由于香客太多，无意中碰了一下大堂的钟，和尚马上上前就骂，索要一百块钱，香客赔理不行，和尚马上就打，出言加恐吓如不给钱，就让香客下不了山，你一言我一语就动起手来。 也有网友称是游客偷了捐款箱里的100块钱，才发生打架事件的。对此记者拨打了安徽九华山咨询中心的电话，工作人员称和尚打游客确有其事，大概发生在一个多礼拜以前了，不过事因不是网络上叙述的那样，此事已经交由当地派出所处理，当地池州市政府也很重视，参与其中在做调查，稍后会向社会汇报事因及其处理情况。<br />
<br />
<br />
　　无论真相如何和尚群殴游客让人愤<br />
<br />
<br />
　　对此有网友认为钟是法器，非重大法会是不能乱敲的，庙里的钟摆在特别显眼的地方，明码标价地吸引游客随意敲击，有借佛敛财的嫌疑，也有网友认为钟放在那里，谁都乱敲，不太合适，所以注明善款100元，也是功德所致，既然敲了就应该捐赠善款。网友“晨梦”认为无论真相如何，出家人这样大打出手，群殴游客，实在让人气愤。华商网<br />
