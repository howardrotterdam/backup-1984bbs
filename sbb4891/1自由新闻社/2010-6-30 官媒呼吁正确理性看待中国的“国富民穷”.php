													<h2>官媒呼吁正确理性看待中国的“国富民穷”</h2>
						
						
																											<div id="postmessage_712407" class="t_msgfont"><a href="http://news.163.com/10/0629/21/6ACIIROC000146BC.html" target="_blank">http://news.163.com/10/0629/21/6ACIIROC000146BC.html</a><br />
<br />
<strong>新华网：正确理性看待中国的“国富民穷”</strong><br />
2010-06-29 21:26:04　来源: 新华网(广州)<br />
<br />
核心提示:针对近期“国富民穷”的讨论，分析人士指出，从相对数来看，中国财政收入比重并未过高；通过转移支付、加大民生投入可以推进“国富”向“民富”转化，两者并非此消彼长的关系；随着经济发展以及收入分配制度完善，中国将实现“国民共富”。<br />
<br />
<img src="http://img1.cache.netease.com/cnews/2010/6/29/20100629225343b12e3.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
新华网北京6月29日报道 依据今年前五个月的全国财政收入数据，有预测认为2010年中国全国财政收入总额或达8万亿元。针对此预测，有关政府财政收入比重过高、企业和居民负担过重、“国富民穷”的讨论迅速占据中国媒体的版面、时段和页面。<br />
<br />
分析人士指出，从相对数来看，中国财政收入比重并未过高；通过转移支付、加大民生投入可以推进“国富”向“民富”转化，两者并非此消彼长的关系；随着经济发展以及收入分配制度完善，中国将实现“国民共富”。<br />
<br />
财政部财政科学研究所所长贾康29日接受记者采访表示，与世界平均水平相比，中国财政收入占GDP比重并没有过高，即使在发展中国家也是属于中等偏低水平。<br />
<br />
他说：“预估的8万亿元是绝对数，全面考量财政收入问题，更重要的指标是相对数。中国财政收入占GDP比重在20％左右，这在发展中国家属于中等偏低水平，并且明显低于经合组织国家平均水平。即使按全口径财政收入比较，中国政府集中财力相对数在发展中国家中也不超过中等水平。”<br />
<br />
2007年到2009年，中国财政收入占GDP比重分别为19．9％、19．5％和20．4％。按全口径财政收入计算，也就是公共财政加上政府性基金收入、国有资本经营预算收入和社会保险基金收入，三年来中国政府收入占GDP比重分别为27．6％、27．9％和30．0％。<br />
<br />
据统计，财政收入占GDP比重的世界平均水平为40％，即使是发展中国家，这一比重的平均水平也达35％。<br />
<br />
中国目前仍处于高速发展过程中，需要大量的建设资金。目前中国的财政赤字占GDP的比重在3％以内，累计国债余额占GDP比重在20％左右，地方政府也有大量显性和隐性债务，因此，中国政府的财政收入还谈不上宽裕，更不用说富裕。<br />
<br />
当前，造成部分群众不公平感的是居民收入占国民总收入比重连年下降的趋势。数据显示，1996年至2007年，中国居民收入在国民收入分配中的比重从69．3％下降到57．5％，累计下降11．8个百分点。<br />
<br />
中国的储蓄率从1998年前后的37．5％升至2007年49．9％，其中企业可支配收入占国民可支配收入的比例从1997年的13％升至2007 年的22．5％，而同期政府可支配收入的占比仅上升2个百分点。<br />
<br />
中国人民银行行长周小川指出，目前，中国企业储蓄占GDP的比例已处于全球高水平，这与经济转轨过程未能充分解决企业成本／利润扭曲密切相关。企业未将成本扭曲形成的超高利润转移成工人养老、医疗和保险等支出，导致企业储蓄显著上升。<br />
<br />
1996年至2007年，在劳动者报酬比重大幅下滑同期，企业利润所占比重却从21．2％升至31．3％。目前，作为中国居民收入主要渠道的工资收入，在生产要素中的分配比例偏低，仅占企业运营成本的不到10％，远低于发达国家的50％。<br />
<br />
“从绝对数来看，近些年来中国居民收入增长还是很明显的，但从相对数来看，社会财富这块‘蛋糕’切分，企业收入增长更明显，政府收入则是恢复性增长，而且一部分作为转移支付用来支持低收入群体的收入增长。我相信随着政策优化和经济发展，居民收入比重会逐步提高。”贾康表示。<br />
<br />
国家税务总局科研所研究员张培森指出，虽然中国政府财政收入连年增长，但财政税收安排明显体现了向民生倾斜、低收入者倾斜的力度，“国富”与“民富”并非此消彼长而是相互促进的关系，是有利于国民收入合理分配的。<br />
<br />
国际金融危机爆发后，中国决定实施两年新增4万亿元的投资计划，2009年安排中央投资9243亿元。其中，保障性安居工程、农村民生工程和农村基础设施、社会事业投资占44％，自主创新和结构调整、节能减排和生态建设占16％，重大基础设施建设占23％，灾后恢复重建占14％，其他公共支出占 3％。<br />
<br />
2010年中国共安排中央政府投资9927亿元，在投资安排上继续向“三农”、保障性安居工程、卫生教育等民生领域倾斜，向中西部地区、革命老区、民族地区、边疆地区、贫困地区倾斜，继续支持节能环保、自主创新和技术改造。<br />
<br />
知名经济学家、中国国家发改委经济研究所研究员常修泽指出，收入分配是一个相当复杂的制度体系，其中蕴涵着经济社会的深层次问题。在收入分配上要防止陷入拉萨尔的“分配窄圈”，单纯“在分配问题上兜圈子”，而是要超越分配问题，从更深层次的制度层面来思考问题。<br />
<br />
全国总工会副主席、书记处书记乔传秀日前表示，力争到2012年基本在各类已建工会的企业实行集体合同制度，全面推行工资集体协商，着力提高一线职工的工资收入水平。<br />
<br />
人力资源和社会保障部的报告表示，将继续从“限高、扩中、保低”的思路出发，采取措施推动建立企业职工工资正常增长机制。<br />
<br />
国务院总理温家宝在今年的政府工作报告指出，“我们不仅要通过发展经济，把社会财富这个‘蛋糕’做大，也要通过合理的收入分配制度把‘蛋糕’分好。”<br />
(本文来源：新华网 作者：刘欢 李云路 韩洁)</div>

							
							
							
							
