核心提示：据外媒报道，美国政府正在中国和伊朗秘密测试一种新技术，该技术能让两国民众避开政府的互联网审查措施。据悉，中国在互联网新闻检查方面世界领先，实行大约60项法规管理互联网。<br />
<br />
新华网8月18日报道&nbsp;&nbsp;据英国《每日电讯报》网站14日报道：美国政府正在中国和伊朗秘密测试一种新技术，该技术能让两国民众避开政府的互联网审查措施。<br />
<br />
这个名为FOE(feed over em ail)的系统利用能绕开网页屏蔽技术的新技术发送新闻。<br />
<br />
负责美国政府对外广播事业的独立政府机构广播事业管理委员会的一位高级官员透露，美国正在中国和伊朗对系统进行测试。<br />
<br />
该系统通过谷歌、微软以及雅虎邮箱提供新闻。<br />
<br />
广播事业管理委员会的信息技术主管肯・伯曼对路透社记者说：&quot;我们的人员正在中国和伊朗对系统进行测试。&quot;<br />
<br />
伯曼对这个尚处于测试初级阶段的新系统没有做过多的介绍。<br />
<br />
在政府经常对新闻媒体进行审查的国家中，互联网已成为民众的一个有力工具，他们可以通过互联网了解重大社会和政治事件，并做出反应。<br />
<br />
一位参与开发FOE的技术人员在电子邮件中说，该系统还能应用于大多数类型的手机。<br />
<br />
俄罗斯《报纸报》网站14日也报道说，美国正在测试一种能够突破互联网监督系统，让极权国家的互联网用户接触到&quot;民主&quot;新闻的技术。进行这一测试的是美国广播事业管理委员会，用44种语言发布新闻的&quot;美国之音&quot;就在该机构旗下。<br />
<br />
机构负责人伯曼说，目前正在伊朗和中国测试这种技术，还准备为缅甸、越南、乌兹别克斯坦和塔吉克斯坦的互联网用户提供民主新闻。<br />
<br />
目前尚不清楚用什么方式可以避开极权国家封闭不合意网络资讯的监督系统。中国在互联网新闻检查方面世界领先，实行大约60项法规管理互联网。国家的互联网接入单位对信息进行过滤。 (本文来源：新华网 作者：陶志彭)</div>

							
							
							
							
