													<h2>李向东地下王国</h2>
						
						
																											<div id="postmessage_740549" class="t_msgfont">李向东地下王国<br />
本文来源于《新世纪》周刊 2010年第15期 出版日期2010年04月12日 财新传媒杂志订阅<br />
一个中国移动省级公司中层干部，如何在中国音乐市场叱咤风云，并构建出一个寄生于中移动的商业王国<br />
《新世纪》周刊 记者 邓海 赵何娟<br />
<br />
&nbsp; &nbsp; * 中移动的彩铃业务将李向东推上了无线音乐金字塔的塔尖<br />
&nbsp; &nbsp; * 中移动主宰数字音乐大半江山<br />
&nbsp; &nbsp; * 数字音乐拯救唱片业<br />
<br />
 <br />
<br />
　　中国移动音乐基地掌门人李向东携巨款出逃，3月底已经中移动内部人士证实。一位接近中移动的消息人士称，春节前审计署已派中移动内部审计人员赴四川查账，3月下旬，审计人员正式找李谈话的第二天，李向东就搭凌晨最早一班飞机飞往深圳，之后去向成谜，一说李从香港转道去了澳大利亚，一说去了加拿大。“中移动内部，特别是省级公司，现在人心惶惶。”中移动总部一位内部人士说。<br />
<br />
　　但这位中国移动四川分公司（下称四川移动）前数据部总经理、无线音乐运营中心总经理的出逃，震动最大的不仅是通讯业，还有音乐等相关产业界。<br />
<br />
　　李向东的职位在中国移动可能根本排不上号，但因其主管的无线音乐业务对正在走下坡路的唱片业举足轻重，他在唱片界已俨然成为“教父”级人物。 2006年，由李向东主导的四川无线音乐基地正式挂牌运营。这个已耗资4亿元左右搭建起来的无线音乐基地给中国移动带来巨额收益的同时，也激活了当时一潭死水的中国唱片业。凭借近4亿无线音乐用户，中移动的无线音乐现在一年创造着超过220亿元的销售业绩。<br />
<br />
　　中移动无线音乐的发展，给李向东带来的不仅是地位的提升，因为唱片公司、彩铃制作和分销等各类增值业务公司能否被纳入中移动的销售体系，都是中移动数据部说了算，李向东更被业界视为财神，成为大家争相拉拢的对象。<br />
<br />
　　而李向东则以基地为核心，通过增值服务提供商（SP）的选择将亲朋好友、老乡同学安插左右，构建了一个寄生于中移动的灰色王国。<br />
<br />
独立王国<br />
<br />
　　以前就颇有几份像军事基地的无线音乐基地，在李出逃之后更加戒备森严。几乎每栋小楼下都站着保安，随时准备对陌生的进入者进行盘查。每层楼、每个通道里都有电子眼监视。每层楼还设有哨卡、门禁。外来者进出困难。而李向东和他的灰色王国就隐身在这层层哨卡之内。<br />
<br />
　　45岁的李向东，于1965年5月14日出生于江苏启东县，曾就读电子科技大学。自2001年起，到2010年案发离职，李向东担任中国移动四川公司数据部负责人长达十年时间，在四川移动时间久、根基深、关系广。<br />
<br />
　　李向东所兼任的无线音乐运营中心主要负责四川移动无线音乐基地建设。该基地建于2006年，是中国移动进军互联网业务布局中的“全曲（音乐）”“视频”“支付”“阅读”“游戏”五大基地之一。<br />
<br />
　　一位接近李向东的人士向本刊记者透露，随着无线音乐基地的扩建与壮大，它逐渐从四川移动数据部剥离独立出来，至2009年下半年几乎完全独立成一个部门，财务也独立核算。顶着四川移动数据部总经理头衔的李向东实际上已把主要精力都放在无线音乐基地，“整个基地目前已有200来号人”。<br />
<br />
　　李向东的上级领导、四川移动的一位高层说，李向东案事发突然，究竟他因为什么被审计机构调查，他也不清楚，目前整个四川移动都在等待调查结果，数据部未受影响。<br />
<br />
　　据知情人士透露，2009年，移动内部即出现了对李向东操纵SP公司并从中获益的举报，举报内容递交至包括中国移动总部在内的多个主管部门。<br />
<br />
　　直到4月7日，本刊记者拨打李向东电话时，依旧能够打通，但是处于无人接听状态。<br />
<br />
　　但李向东案发，直接诱因还是审计。在央视曝光中移动“涉黄”之后，中移动对SP的管理遭来一片批评之声。中国审计署、财政部、国家税务总局于今年年初轮番进入中国移动审查，与移动运营商们合作的SP也首次被纳入审计范畴。中国移动总部一位人士透露，审计署要求与运营商合作的SP提供相关材料，配合调查，至今审计仍未结束。<br />
<br />
　　有四川移动内部人士告诉财新记者，在李向东不知所终多日后，四川移动主要负责人曾在3月末召开移动公司内部会议，对内通报此事，李向东的妻子姚红也参加了这次会议。<br />
<br />
　　此外，也有跟李向东往来多次的SP运营商表示，最近几年，中国移动无线音乐基地一直在扩建，而通过这种基地建设，可以扩大其预算。“先是赚钱，然后是花钱，两种方式都对个人有利。”该人士表示，李向东很有头脑，做事精于设计，基地建设或许也是其出走的原因之一。”<br />
<br />
一个音乐“教父”的诞生<br />
<br />
　　集数年之功，李向东在中国无线音乐界建立了“教父”一般的地位，亚洲四大唱片公司全球高层每次来华几乎都要拜会李向东。即使是王力宏、蔡依林这样的台湾大牌明星来大陆推广，也必须要赴川拜会李向东。<br />
<br />
　　李向东主持四川移动数据部、并建设移动无线音乐基地的十年，正好是移动增值业务从诞生到繁荣、从混乱逐步规范的十年。中移动对无线音乐寄予厚望，希望在四川打造一个面向全国用户的无线音乐基地，全网统一运行、统一管理、一点接入、全网服务，建立一个以中移动主导的无线音乐价值链。<br />
<br />
　　这个价值链的核心，就是彩铃。唱片公司是内容的主要提供者，即一般所谓的CP（内容提供商）。<br />
<br />
　　2002年，彩铃开始在韩国流行，随后在亚太地区取得了辉煌的成功。第二年，中移动率先将彩铃引入中国，李向东是关键人物。当年在韩国学习手机彩铃业务的四川移动数据部主任李向东，2004年回国的第一件事就是申请在成都建立中国移动音乐基地。<br />
<br />
　　2004年中国移动在无线音乐方面收入15.8亿元，2005年翻了将近1倍，达到30多亿元。由于李向东推出的彩铃业务贡献突出，四川移动在与其他几个省的竞争中脱颖而出。2005年8月，四川移动成为中国无线音乐备选基地，李向东的12530门户和中央音乐平台运营方案也获得推广机会。翌年 3月，四川移动无线音乐产品基地正式挂牌运营。<br />
<br />
　　在一片萧条的唱片业中，数字音乐的崛起成为惟一的一抹亮色。在2004年之前，中国乃至全球音乐产业增长率已几近停滞，最近三年，全球音乐产业销售收入更是连续负增长，但从2005年开始，数字音乐增长却一直保持稳定，收入明显提升，收入占比也逐年递增。<br />
<br />
　　“2006年之前，中国音乐市场几乎就要死了。”一位原环球唱片数字音乐项目负责人称，是四川音乐基地的建设改变了局面。他介绍说，在此之前，中国的唱片公司已急剧萎缩到仅存数十家，这几年又扩大至三四百家，很多公司完全靠中国移动无线音乐业务养活。<br />
<br />
　　据本刊记者获得的四川移动数据部内部统计，中国移动的无线音乐业务已占了中国无线音乐市场80％左右的市场份额，彩铃使用人数达到3.87亿，完全建立起霸主地位。<br />
<br />
　　其中，除华纳外，另三大唱片公司――索尼、EMI、环球唱片的总收入中，从中国移动分得的收入占40%-50%，其他唱片公司则有70%-80%的收入来自中国移动。<br />
<br />
　　与巨大的市场贡献和份额相对应的，是音乐基地及其创始人李向东的地位迅速提升。而中国移动的“基地”战略更将李向东推上“教父”的宝座。<br />
<br />
　　中国移动过去一直是中央音乐平台和各省平台同时并行。一首彩铃既可以上中央音乐平台，也可以上各省本地通道。原则上，中央音乐平台的彩铃版权只能隶属一家唱片公司，而在本地通道上，一首彩铃版权可转售给多家SP。<br />
<br />
　　在中国移动的“基地”战略下，四川移动的中央音乐平台被打造成音乐集成平台，其前台是12530.COM音乐门户网站，为客户提供包括音乐搜索、彩铃定制、音乐下载、音乐资讯等多种与音乐相关服务。用户通过手机就能获得最新的音乐、排行榜歌曲。该平台还为中国移动下属品牌“中国无线音乐排行榜”提供支持。<br />
<br />
　　据本刊记者了解，四川移动在音乐基地具体运营中主要采取外包模式。在内容上，四川移动可与四大唱片公司，包括滚石移动公司直接合作，其他则委托SP公司集成内容；在技术支撑上也主要由SP公司负责。<br />
<br />
　　为规范管理各省重复投入的音乐平台，中移动于2010年1月1日正式实施无线音乐内容集中管理，所有彩铃内容由设在四川的音乐基地统一引入，各省公司不允许自行新增任何本省彩铃内容。<br />
<br />
　　无论是SP还是CP，其能否进入中移动网络平台的关键，都系于四川移动数据部和音乐运营基地主管一身。时势之下，李向东被推上了音乐市场的金字塔尖，成为各方争相讨好的对象。“在2003年后，因为李向东喜欢打网球，很多SP负责人也开始练网球，希望能和其套近乎。此后，李向东开始打高尔夫球，SP们就开始练习高尔夫。”一位业内人士透露。<br />
<br />
　　尽管很多彩铃内容提供商对李向东及其无线音乐基地的分配方式颇有微词，很多人仍认为他“确实有能力，愿意学习，懂行”。一家中国移动无线音乐基地的CP负责人认为，中国移动所有的基地中，只有音乐基地异军突起，除彩铃的市场潜力，跟李向东也很有关系，“权力加上懂行，造就了李向东的地位”。<br />
<br />
影子公司<br />
<br />
　　在围绕着无线音乐形成的巨大利益链条中，SP因提供业务的技术含量不高、业务性质单一，具有很强的可替代性，竞争十分激烈。也因此，SP的竞争在很大程度上沦为了关系的竞争。这里很快成了李向东的私有领地。<br />
<br />
　　财新记者调查发现，在李向东担任四川移动数据部负责人的十年时间里，有多位亲朋从其分管的移动数据领域获得业务，相关关联公司主要分布在李向东的工作地四川及其老家江苏，也有的注册在北京，其共同特点是股权变更频繁。开始时股权分散，随着移动增值业务收入的提高逐步集中。以成都音信互动信息技术有限公司（下称音信互动）为例，参股人俞卫中与李向东关系紧密，系李之同乡和大学同学。<br />
<br />
　　音信互动成立于2005年1月18日，主要从事电信增值业务的咨询服务等，与四川移动数据部有长期合作关系，主营业务包括与掌上天府充值卡、天府快讯充值卡、动感地带、随意呼等相匹配的SP服务。<br />
<br />
　　该公司由成都创思特科技发展有限公司（下称创思特科技）、俞卫中、秦兴兰三方发起成立，俞卫中为公司法人和总经理。在注册资本中，创思特科技投资额37.5万元，占股75%；俞卫中投资7.5万元，占股15%；秦兴兰投资5万元，占股10%。2007年，俞卫中将其股份转让给黄克芳。<br />
<br />
　　知情人士透露，李向东祖籍江苏启东县，李向东父亲生前一直在启东县工作。音信互动的发起者之一俞卫中不仅是李向东老乡，而且在1983年两人同时考入电子科技大学，李向东就读于无线电专用机械设备专业，俞卫中就读于无线电技术专业。<br />
<br />
　　俞卫中在接受本刊记者电话采访时否认与李向东相识，称双方素无往来，甚至与四川移动的数据部也早已没有业务往来。知情者告诉财新记者，尽管俞卫中在2007年已将自己的股份转让给黄克芳，但实际上是“左手换右手”。根据音信互动工商资料和俞卫中学籍档案显示，黄克芳和俞卫中二人登记的家庭地址为同一地址，都是启东县天汾乡中心小学，前者今年71岁，后者今年45岁，关系非同一般。<br />
<br />
　　另外一家跟四川移动业务往来广泛的公司――北京迅捷英翔网络科技有限公司（下称迅捷英翔）的股权变更也令人眼花缭乱。<br />
<br />
　　迅捷英翔成立于2004年4月，注册资本金200万元，主要为无线音乐俱乐部、12530音乐门户网站、梦网wap音乐门户、12530999语音门户等提供产品开发、内容组织、技术支持、营销推广等运营服务，这些业务基本全部来源于李向东掌控的移动无线音乐基地。<br />
<br />
　　在四川移动数据部的合作伙伴中，TOM.COM是内容集成商，主要代理版权。但它与负责无线音乐运营维护的主要SP和李向东之间的关系紧密在四川移动内部为人熟知。<br />
<br />
　　2004年4月发起成立时，迅捷英翔的法定代表人是洪亮，投资人包括洪亮、盛勇、仇卫民、严珊、戴坚、陈政、杨琨、刘明海；当年8月，就变更为华如秀、盛勇、戴坚、王霆霆。<br />
<br />
　　在变更后的投资者当中，华如秀正是之前出现在音信互动的创思特公司的法人代表，而盛勇和王霆霆两人此时均在TOM在线任职，其中盛勇为TOM在线总裁助理，王霆霆则是TOM在线首席执行官王雷雷的堂弟。<br />
<br />
　　工商资料显示，2004年，迅捷英翔亏损9万多；2006年无线音乐基地正常运营后，其税后利润猛增至889万元；2007年全年销售收入翻倍，达到5287万元，利润140万元；2008年，销售收入达到1.3亿元，利润总额651万元。<br />
<br />
　　然而，就在2008年公司业务大幅好转时，创思特的华如秀再次激流勇退，与其一起退出的包括盛勇和王霆霆，2008年九十月间，他们将股权陆续转给TOM在线原副总裁蒲东皖。蒲东皖同样来自江苏，也是李向东的老乡。有多位业内人士证实二人关系亲密，但如同俞卫中一样，蒲东皖也否认对此事知情。<br />
<br />
权力从何而来<br />
<br />
　　作为一个在中国移动省级公司的中层干部，李向东何以一手在唱片业呼风唤雨，一手操纵下游具体业务操作？一些业内人士将矛头指向中移动的“基地”模式。<br />
<br />
　　在中移动提出“基地”模式之前，中国各大电信运营商包括中国移动都是分省而治，每个业务的运营主体都是省分公司，而无线音乐基地的建立是对这种模式的颠覆。中移动改革的初衷是集中采购，集中管理，提高效率，但实际操作中因将内部资源更加集中于一个部门和一个人之手，为相关者创造了更大的寻租空间。<br />
<br />
　　在李向东手中，掌控着中国移动31个省近4亿多用户的无线音乐应用，SP和CP要与中国移动就无线音乐合作，李向东是绕不过的人。李向东最早提出中央音乐平台与音乐内容资源方（唱片公司）按照五五分成，SP分成则根据不同公司具体谈。合同谈定后，将递交四川移动法律部门审查，主管副总经理处盖章。但业内人士表示，这类审查和盖章目前形式大于实质，对于在四川移动有十余年根基又是无线音乐基地创始人的李向东而言，形同虚设。<br />
<br />
　　中移动对SP管理的分级模式和移动搜索排名亦产生了巨大的灰色监管空间。中移动根据合作SP的综合实力与增长性，将之分为优秀和普通两个等级，其中优秀又分为A、B、C级别。中国通信业资深人士项立刚评论说，“理论上资源会越来越向高级别SP倾斜，低级别的慢慢被淘汰。但实际上主要决定于与李向东的关系亲疏。”<br />
<br />
　　“无线音乐排行榜”等类似的产品排行榜单也是如此。无线音乐各门户界面上的推荐直接影响到用户下载量，即收益。项立刚称，目前电信公司普遍监管不严，冲榜比较笨的办法是SP“自消费”刷榜，有能量的SP凭关系就能排到前面。<br />
<br />
　　“这种商业模式（‘基地’）的根本特点就是垄断，有通达客户的渠道，掌握几亿手机客户，钱要从这里收，任何增值产品都要通过它转发出去。”一位接近中移动的业内人士表示，这种集中管理只能带来更大的集权腐败。<br />
<br />
　　但在李向东案发之前，基地模式被认为有利于集中资源发展新兴业务，还引来了中国电信和中国联通模仿。中国电信在广州建立了全网数字音乐运营中心。中国联通略有不同，成立独立子公司――中国联通音乐公司负责全网音乐运营，但因为唱片公司合作积极性不高，中国联通尚未打开市场。<br />
<br />
　　原环球唱片无线音乐负责人向本刊记者表示，唱片公司乐于接受现在的移动模式，主要因为摆脱了过去对于SP的依赖，减少了“压榨”层级，但很多CP都对现在的分成模式不满。<br />
<br />
　　该负责人介绍，CP与基地直接五五分成，主要是来自信息费，但由于用户活跃度不高，具体算下来很可能拿到手上的很少。比如一个人定制了彩铃，一个月5元，一年支付60元，但可能他只支付2元下载了一首歌，意味着CP只能从移动总计62元中收入1元。<br />
<br />
　　从四川移动内部统计看，信息费仅占中国移动无线音乐增值业务收入的14%。<br />
<br />
　　中移动在SP领域遭遇的问题并不新鲜，是很多大企业都面临的难题。将李向东的出现归罪于“基地”模式并不完全公平，因为只要中移动仍是中国移动通信市场的垄断者，想在这一领域发展的CP和SP就必须找中移动合作。即使没有“基地”，由各省移动来运作，其结果无非是各唱片公司和SP要拜会的码头从一个“李向东”变成多个。<br />
<br />
　　解决类似的“大企业病”，真正走出新的管理模式的是手机终端厂商。以苹果为例，苹果推出了在线音乐商店iTunes，曲库内容超过800万首，是全球第一大网络音乐零售商。苹果网上商店向所有开发者开放，除了每年99美元的注册费，没有其他费用，对个人和大制作公司一视同仁。<br />
<br />
　　这种开放的平台显示了强大的生命力。而中国移动虽然有巨大的用户规模基数，但用户活跃度不高，其设置的审批式的运作流程，也形成了“一夫当关”的局面。<br />
<br />
　　3月29日，四川移动正式下文将李向东免职，任命四川移动建设中心总经理陈健骥接替李向东原职。这意味着李向东灰色王国的终结，但还有很多其它的类似寄生国仍然活着。<br />
<br />
　　本刊记者宫靖、曹海丽、王姗姗对此文亦有贡献</div>

							
							
							
							
