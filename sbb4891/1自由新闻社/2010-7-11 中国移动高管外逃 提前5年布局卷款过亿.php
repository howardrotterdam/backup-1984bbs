																											<div id="postmessage_733356" class="t_msgfont">移动高管外逃：提前5年布局，卷款过亿<br />
2010-07-11 10:34:41　来源: 时代周报(广州)　<br />
李向东的座驾是豪车两部：奔驰S级W12，路虎揽胜。另有一部价值80万的雷克萨斯SUV，但不事张扬、锋芒从不外露的李向东平时都是乘坐公司的专车。“李向东留下的网球拍和高尔夫杆一大堆，如果把他自己用不了送亲朋好友那些全加起来，差不多能装满一个小箱柜货车。”<br />
据确切消息，四川移动数据部原总经理、中国无线音乐运营中心总经理李向东是3月25日晚飞离成都，距离李华被“双规”整整三个月，其间四川移动公司内部丝毫未向外界透露一点风声，如果不是一位和四川移动音乐基地有合作关系的某公司总经理王某某不经意之间透露出此事，估计中国移动有史以来最大的一笔携巨款出走案至今还不为外界所知。<br />
<br />
李向东和李华<br />
<br />
李向东是在自己即将被重用提拔之前出走。<br />
<br />
正是因为把无线音乐基地做得太好了，以致引起了中国移动总公司的重视和青睐，总公司计划在6月之前，把无线音乐基地划拨由集团公司直管，李向东的职务级别也即将由地市总经理提拔为省级总经理。<br />
<br />
3月25日，国家审计署派小组进驻四川移动，当日下午约请李向东谈话，当晚，李向东连夜出走。3月26日上午，四川移动在成都高升桥总部内向员工传达了这一消息，在场者无一不发懵。四川移动消息灵通人士称，事实上，国家审计署小组找李向东谈话，不过是在即将升迁前的一次工作谈话，如果早有准备，就不会采取这种方式，否则李向东不可能这么轻松离开中国大陆，现在看来，一直操盘的李向东在和国家审计署小组谈话后，完全成了惊弓之鸟，当夜即飞离成都。<br />
<br />
种种迹象表明，李向东是在25日搭乘海航HU7752（20 : 00起飞）或南航CZ3434（21 : 15起飞）航班离开成都直飞深圳。在深圳，曾有认识他的人似乎是看到他搭乘出租车出现在水围附近，那里是通向皇岗关口的道路，24小时均可通关。<br />
<br />
事实上，更换国籍是李向东整个一盘棋中的一部分，在过关或登机时，身份证和护照已经不起任何作用―李向东很可能是持加拿大绿卡迈过一道道通道。在带走几亿元之前，不把境外这些事情做好，李向东也许就不是李向东了。<br />
<br />
正是他，于2005年4月，向李华大力陈述，再由李华向中国移动总公司提出申请，在成都建立中国移动音乐基地，将各省、市及自治区的彩铃接入权收归基地，而中移动总公司也欲借此增加话语权，进而主导产业链。<br />
<br />
而真正让李向东一炮走红、崭露头角的是2006年春节，他麾下基地12530网站与央视“春晚”同步直播，此后，中国移动无线音乐基地就一直在这个产业链中牢牢占着主导地位。<br />
<br />
接近李向东的人向时代周报记者介绍，李向东之所以敢第一个吃移动增值音乐基地这个“螃蟹”，是和他内敛的性格有关，看问题准和透是周围人对他的最多评价。<br />
<br />
在怂恿四川移动总经理李华向总公司建议音乐基地时，李向东就是看透了当时中国移动各个分公司采取的是各自为政、价值链角色多而繁杂的背景下，各分公司采取的所有招数看起来都是小打小闹的游击战。各分公司投入巨大，市场拓展却是混沌而参差不齐，整合无疑是最佳选择。<br />
<br />
而这一年，李向东刚过41岁。<br />
<br />
11年前，1999年，中国电信系统改革，中移动从电信中剥离出来，掌控中国移动四川分公司的李华苦于人手不足，风华正茂的李向东在偶遇中被李华慧眼识中。<br />
<br />
从李向东在移动的履历表可以看出，入职移动开始，李向东先是担任公司市场部副经理，2001年，数据部从市场部分离出来，李担任数据部总经理，这一干就是四年。<br />
<br />
2005年，李向东的人生到了一个转折点―李华向总公司汇报音乐基地战略构想后，李向东一举击败几家沿海或发达城市分公司，把无线音乐基地带入到大西南，落户成都。<br />
<br />
李向东的远见没错，至2009年末，据中国移动总公司财务报告显示，由无线音乐基地带动的音乐增值服务，整个移动网收入达到了220亿元，这个数字占中国移动总公司全年收入的4.2%、增值业务收入的16.8%，它比四川移动全年的传统收入总额还要高出很多。也就是从这一时期起，人们才开始把中移动和石油、烟草等强势国企等同起来。<br />
<br />
李向东的棋局<br />
<br />
“国企官员现在是高危行业。”这是时代周报记者和四川移动几位新朋老友有意或无意交流时，在重复的语句中，出现频率最高的一句话。<br />
<br />
细细想来李向东在四川移动的轨迹，此话就真的毫不夸张，或许可以说完全是实事求是。<br />
<br />
一位SP（增值服务提供商）供应商向时代周报记者介绍，中国移动音乐基地设在成都后，全国的SP资质，都由李向东一人审批，为能拿到一个SP资质，他曾几次用各种手段“接近”李向东，但都不为所动。他仿佛是不食人间烟火一样，接触几次后，弄得一鼻子灰，最终只好“灰溜溜”放弃。<br />
<br />
这位SP供应商幽幽地说，在音乐基地前期，李向东完全是独来独往，尤其是和单位同事，几乎没有任何交往，曾经的朋友也都渐渐淡出他的生活圈子。单凭这点，籍贯原本在上海的李向东倒是更符合成都本地人性格。据记者在成都媒体十几年观察所知，凡是在单位独自进出，语很少、话不多的，多为成都本地人，反而是外市县来的人，更容易拉帮结伙。<br />
<br />
但李向东的“两袖清风”并没有持续多久。基地成立两年后，李向东虽然和单位同事没有过多接触，但“外面”的人却认识太多―都是对他有所求之人，每位SP服务商，为了取得一个资质，都必须得到他的批准―不仅仅是成都、四川，全国任何一家SP服务商，如果没有李向东的批准，就无法进入移动音乐增值这个链条，面对每年几百亿元的巨大商机，谁都不想错过。<br />
<br />
李向东此时的权力巨大，他的“外部”变化开始出现：打网球，然后是高尔夫。李向东打网球，很多SP服务商们也开始学网球，李向东网球打腻了，又开始玩高尔夫，SP服务商们也弃“网”奔“高”。一直到出走之前，每逢周末，李向东接到的挥杆邀请从来不断，要么是北京、要么是海口，来回都是头等舱。<br />
<br />
这时，李向东的座驾是豪车两部：奔驰S级W12，路虎揽胜。另有一部价值80万的雷克萨斯SUV，但不事张扬、锋芒从不外露的李向东平时都是乘坐公司的专车，这些车子要么搁置，要么是送给由亲友开。<br />
<br />
“李向东出走后，留下的网球拍和高尔夫杆一大堆，如果把他自己用不了送亲朋好友那些全加起来，差不多能装满一个小箱柜货车。”一谙知内情的人这样告诉时代周报记者。<br />
<br />
和其他落马的官员往往卷入色情事件相比，在生活上，深邃兼有超强自控能力的李向东没出现任何“绯闻”―目前尚未有此话题传出，在个人生活方面，李向东属于绝对“清廉”。<br />
<br />
“李向东本人颇为清高，一般人是不放眼中。”熟悉李的人这样评介昔日领导。<br />
<br />
据说，李向东和妻子姚红关系相当不错，虽然两人婚后一直未有“结果”，但这并没防碍两人情感，2004年，正值花样年华、毫无任何理由的姚红突然从四川电信集团实业有限公司副总经理的位置上退下并离职，旋即去了加拿大。<br />
<br />
可以推测，李向东那时就在准备后路，从到音乐基地起，就开始下一盘“很大很大的棋”。<br />
<br />
李向东事发后，他的同事才把姚红离职和李向东连夜出走联系起来，这一思考后，都不由自主倒吸一口冷气―这家伙，无论做好还是做坏，都是个干大事的料。<br />
<br />
由于案件迄今尚未对外界公布，记者还无法完全了解诱发李向东出走的直接原由，但可以肯定的一点是，李向东利用音乐基地的平台，有自己控制的子公司，用这个链条寻租敛财。从音乐平台2009年有200多个亿收入看，李向东通过他的公司带出去的，起码在1个亿以上，从他本人性格上看，李向东就是个“下大棋”的人了。<br />
<br />
坊间有消息传出，并有媒体报道，说李向东携有4亿-6亿元出境，据记者得到比较可靠的消息，李向东带出去的资金，在3.4亿-4.5四亿之间。在所有的猜测中，现在的焦点都指向成都娱音科技有限公司，从现在所掌握情况来看，娱音科技是和音乐基地合作的最大受益者。时代周报记者找到一位SP运营商，据他透露，从无线音乐业务接入到无线音乐基地统一运营过程中，娱音科技负责了音源审核、音源制作以及中小版权公司音乐的内容接入。<br />
<br />
单是无线音乐业务接入到无线音乐基地统一运营这一块，就为娱音科技带来了广袤财源。根据音乐基地和娱音科技签订的双方协议，每完成一首铃源制作，无线音乐基地需要向娱音科技支付20元，如果接入音乐内容，则按SP模式进行分成。<br />
<br />
从2009年9月的公开数据不难看到，无线音乐基地已备有130万首音源，按照每首20元计算，娱音科技至少已经获得2600万元的制作收入，此外，娱音科技获得的SP分成亦蔚为可观。<br />
<br />
这位人士还向时代周报记者透露，娱音科技还成立有正版词曲版权数字营销的子公司，代理华纳盛世、富士太平洋、雅琪音乐等版权公司业务。此外，他们除和中国移动音乐基地合作外，娱音科技还和中国电信、中国联通、诺基亚、腾讯、百度、谷歌等多家公司都有合作。<br />
<br />
李向东的棋确实下得非常大。<br />
