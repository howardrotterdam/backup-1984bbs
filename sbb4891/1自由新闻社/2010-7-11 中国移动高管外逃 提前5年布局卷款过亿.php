<br />
李向东携巨款潜逃未遂，国家有关部门正对其进行立案调查<br />
<br />
　　【《财经》记者 李微敖 】针对高管李向东携款外逃一事，中国移动通信集团四川有限公司（下称四川移动）一位主要负责人向《财经》记者证实，李向东潜逃未遂，现已归案。<br />
<br />
　　李向东案发前任四川移动数据部总经理、无线音乐运营中心总经理。2010年3月底以来，“李向东失踪”的传言开始在网络上不胫而走。通信业界则传闻，李向东携带4至6亿元人民币潜逃至加拿大，一说为澳大利亚。<br />
<br />
　　四川移动主要负责人告诉《财经》记者，近日，包括审计署在内的有关部门，已就李向东所涉问题展开调查。<br />
<br />
　　与此同时，《财经》记者从广东移动内部人士处获知类似讯息。该人士称，国家审计署在广东移动的专项审计工作，可能涉及到四川移动的相关高管。国家审计署专项审计调查组2010年2月9日起进驻广东移动，进行专项审计调查工作。后者随后要求其省内SP（增值业务提供商，主要是网站）提供2009年岁末资产情况，以便审计署进行统计调查。而此次李向东出事，或由SP业务引发。<br />
<br />
　　4月5日，四川移动内部权威人士向《财经》记者确认，3月29日该公司已正式下文，李向东被免职，该职务由四川移动建设中心总经理陈健骥接替。此前四川移动已召开高层会议，内部通报李向东“失踪”一事。<br />
<br />
　　 4月6日，四川移动一位主要负责人则向《财经》记者证实，目前，李向东已归案，相关部门已对其立案调查。<br />
<br />
　　 该负责人称，四川移动目前只有李向东一人涉案，暂无他人牵扯其中，因此影响“并不太大”。而对于李向东携款金额与潜逃目的地，该人士未作回应。<br />
<br />
　　 1965年出生的李向东，曾获电子科技大学硕士、美国布法罗大学（University at Buffalo）商学院EMBA等学位。2001年，36岁的李向东被任命为四川移动数据部总经理。2004年，负责移动新业务开发的李向东在韩国学习期间，对日益兴隆的手机彩铃业务颇有兴趣，回国后，即积极筹措引入。<br />
<br />
　　 2005年4月，四川移动向中国移动总部申请在成都建立中国移动无线音乐基地；6月，基地获批，李向东旋即被任命为无线音乐运营中心总经理，负责基地运营，面向全国移动用户提供彩铃、个性手机铃声、音乐全曲等无线音乐业务，并得到迅猛发展。2007年，这一基地的产值即已超过140亿元。<br />
<br />
　　在无线音乐产业链中，众多SP高度依赖于李向东掌控的这一渠道；而越来越多的CP（内容提供商，诸如各大唱片公司，也包括音乐歌手）也对此日益重视，李向东成为行业内“执牛耳”的垄断性人物，受到各方“膜拜”，并得到多重荣誉。<br />
<br />
　　 4月6日上午，《财经》记者前往位于成都市高新西区百草路上的“中国无线音乐基地”采访。该基地工作人员称，目前陈健骥已正式上任。与《财经》记者通话时，陈健骥本人拒绝接受采访。■</div>

							
							
							
							
