													<h2>薄熙来感叹月饼价格高</h2>
						
						
																											<div id="postmessage_841538" class="t_msgfont">　　2010年09月23日 重庆日报<img src="http://news.xinhuanet.com/local/2010-09/23/12598600_11n.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
&nbsp; &nbsp; 昨日，中秋节，重庆市委书记薄熙来率商委、民政等部门负责人考察月饼市场，要求做好市场供应和监管，让老百姓吃上可口、放心、便宜的月饼，营造浓厚的传统节日氛围。 <br />
　　解放碑新世纪百货，商品琳琅满目，顾客熙熙攘攘，洋溢着浓浓的节味。散装月饼货柜上，豆沙、枣泥、云腿、五仁各种馅料的月饼一应俱全，价格从一斤10多块到30多块不等，不少市民正在精心挑选。薄熙来走到货柜前，不时拿起月饼，看看品种和价格。“市民最喜欢什么口味的？”“盒装月饼卖得好，还是散装卖得好？”薄熙来问。营业员陈鸿说：“一般自己吃就买散装的，送亲朋好友才买盒装的，老百姓都图个实惠，我们还推出了‘买一送一’活动。”随行的市商委负责人也说：“今年我们组织的5000吨月饼，散装的占了一半，而且价格都不贵，往年的天价月饼越来越少了。”薄熙来说：“月饼口味多，价格便宜，市民才买得起、吃着满意，这样做才是百姓的中秋节。”<br />
<br />
　盒装月饼货柜上，一些港产广式月饼标价五六百块，高的要一千多块一盒。薄熙来拿起一盒：“这个价格好高啊！”一旁的营业员说：“1000多元是标价，今天是中秋节了，打折后只要260元。月饼季节性很强，中秋一过就卖不动了。”薄熙来说：“这个打折都是‘跳水价’了，看来原本的成本价就没那么高，但为什么不打折到二百五呢？！”大家都哈哈笑了起来。<br />
<br />
　　重百超市临江门店也热闹非凡，各式月饼摆满了柜台，有9.9元一斤的散装月饼，也有按个卖的小月饼。正在选购的市民见到薄书记，纷纷围拢过来打招呼。“都喜欢吃什么口味？”“和去年比，今年月饼价位怎么样？”“质量有没有保证？”薄熙来做起了“市场调查”。“月饼甜的好吃。”“价格比去年低了四五块钱。”“质量没问题，我们吃着放心。”大家你一言我一语。一位老大妈还抓住机会提建议：“现在不少人血糖高，应该多生产一些无糖月饼。”薄熙来说：“你的意见商业部门要注意，要让老百姓都吃上可口、放心、便宜的月饼”。<br />
<br />
　　考察中，薄熙来还嘱咐相关部门，中秋、端午、春节、元宵是中华民族的传统节日，老百姓很上心，除了做好市场供应和监管，还要在街道和公园的环境布置、商场销售、产品包装等方面增加咱中华文化的元素，营造更加浓郁的节日氛围。咱重庆有个四川美术学院，这方面人才不少，可以多花些心思，做好了，也会给山城人民增添节日的欢乐。<br />
