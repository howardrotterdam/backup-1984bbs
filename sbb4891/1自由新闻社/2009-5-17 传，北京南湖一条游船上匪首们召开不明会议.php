<br />
<br />
第一个发言的钱理群教授深情地说，他来参加这个会议有两个动因。一是出于教师的良知，20年前，许多学生为中国民主事业献出了生命，他作为教师，未能保护他们，一直心怀内疚，因为保护受难学生是教师的天职，也是北大的传统。90年前的“五四”，当学生被捕，挺身营救的就是蔡元培校长。学生牺牲而不能保护，已经欠了一笔债；学生蒙冤至今不得昭雪，如果再不说话，就是愧为人师。其次，是出于学者的良知。政治家可以不为陆肆平反，而学者却必须将陆肆写入历史，进行学理的讨论与研究。<br />
<br />
<br />
周舵讲了他在1989年从4月中旬到6月4日的经历，其中包含一些不为常人所知的重要事件的细节，这引起与会者的兴趣和各自的回忆，并引发了关于“拒绝遗忘还是保持集体沉默”的热烈发言。<br />
<br />
<br />
徐晓回顾了自己从上世纪70年代起就参与民主运动的经历，并向大家发问：“作为个人，我们可以做到拒绝遗忘，用一切可能的手段延续记忆；但是，面对一个看起来日益强大、甚至可以影响世界格局的政权，面对一个以实用主义、犬儒主义为主流的社会，我们能够在什么意义上坚持与坚守对抗的姿态？”<br />
<br />
<br />
崔卫平认为，“这么长时间，我们对于陆肆集体保持沉默，实际上参与了隐瞒这桩罪行，这使得我们每个人，对于这件事情已经有了一定的责任。”她还问：“在陆肆 20周年之际，我愿意向周围所有朋友提出这样的问题：二十年来的沉默和隐瞒，给我们社会带来的影响是什么？给我们民族的精神和道德带来怎样的损害？而我们自己在工作、生活中所受的损失又是什么？我们还打算继续沉默吗？”<br />
<br />
<br />
莫之许说，20年来抵抗遗忘的斗争获得了显著成果，这既来自于市场化、全球化的社会进程，还来自于互联望网的恩惠。这一胜利的成果已然进入了新一代的生活，当他们遇到权力的暴虐时，总是不由自主地将之与20年前的悲剧联系起来。从而，陆肆日渐成为当下反对垄断权力，反对专制政体的精神资源、象征符号和认同基础，通过这一纽带，越来越多的人走上了追求自由民主之路。<br />
李海谈到了陆肆如何成为自己生命的一部分。他在陆肆之后生活、工作受到了直接影响，付出了巨大的代价。他感到现在需要考虑如何降低这种影响，回到一个正常人的生活轨道。<br />
 <br />
刘自立从历史纵深的维度，以“启蒙、秩序和反革命”为题，从思想文化角度解读从五四到陆肆的重大变化和事件：“从五四开始的国家主义和个体主义争夺话语权，转变成为人民革命；废黜传统的文化虚无主义，又转型为红色革命文化，继而消灭一切文化；1949年代表民主转变成为独裁极权；1966年文革代表极权主义人民运动和极权主义国家统治的结合；改革之1978年，代表极权主义取消革命。后陆肆政权，以建立中国价值，对抗普世价值。”<br />
<br />
<br />
张耀杰论文的题目是“根本解决与点滴改良”，对比了五四新文化运动期间胡适的渐进改良主张与陈独秀、钱玄同、刘半农、鲁迅、周作人、李大钊等人的激进革命态度，认为陆肆时“假如绝食学生及时撤退的话，既给当局提供了考虑其它选项的余地，也可以消解开枪镇压的理由和借口。”并建议现在“维权人士也应该提出‘刚性抗争’和‘韧性抗争’的概念”。<br />
<br />
<br />
秦晖在发言中将陆肆与东欧前社会主义国家的民主运动在运动模式、结果等方面做了比较。他认为陆肆民主运动发生在一个组织资源极度贫乏、政府与民众极度缺乏互信、互动关系的社会中，但中国的学生、市民和知识分子组织较快，诉求和行动都十分理性、和平，干得不错。针对陆肆镇压产生了中国经济大发展的说法，他从历史学者的角度出发，认为不能从一次事件就总结出历史规律，他指出，陆肆镇压与经济发展只有时间的先后关系，而很难证明它们之间存在着因果关系。<br />
<br />
<br />
研讨会多篇论文指出，陆肆天安门事件是当代中国史的一个分水岭，陆肆带来一个视野是质疑中共统治的合法性。田晓青提出，陆肆之前的合法性虽然不是建立在民意的自由表达之上，但有一种借助于革命权威的合法性：自称争取自由民主，反抗反动派的压迫和不义；由宣传造成的想象――人民政府为人民，人民当家作主，专人民的敌人的政；以及实行改革得到拥护产生的合法性。但陆肆的暴力却是纯然非法的，取消了有关合法性的想象，自此，权力之依托从合法性转向有效性（去意识形态化）：暴力之有效性和利益之有效性。改革之道义（政治）目标被取消，党也从意识形态集团转变为利益集团。<br />
<br />
<br />
徐友渔宣读论文时指出，陆肆开枪性质严重，意义深远，旧有的意识形态源远流长、精致牢固，如果没有陆肆的鲜血，人们并不容易挣脱；支撑1989年学生运动的精神资源，相当一部分是出自于中国共产党的意识形态，一个学生运动领袖面临即将到来的残暴镇压时说，要用鲜血来唤起中国人民的觉醒，此话不幸而言中。陆肆事件没有改变中国的政治体制，但它为中国政治体制的改变昭示了紧迫性，并准备了思想条件。<br />
<br />
<br />
陆肆之后社会形势的变化发展以及对于现状的认识和分析，是与会者讨论比较集中的话题。殷玉生认为，陆肆之后，政府的意识形态基本失效，统治靠武力支持下的经济高速发展，这掩盖了几乎所有的矛盾，但从发展中权贵获益多，底层民众获益少，当民众的觉悟和要求提高时，反抗的成本会降低，而一旦出现经济危机，将引发社会动荡进而引发政治转型。王俊秀说，陆肆以后中国的变化有两点，第一，20年来形成了一个畸形改革的“中国模式”，经济上表现为低人权福利保障的出口发展战略，形成了一个资本与权贵结合的新垄断阶层；第二是2003年以后兴起了公民维权运动，《零八宪章》是公民维权运动的进一步发展。<br />
<br />
<br />
梁晓燕谈到了“八九”一代年轻人和今天的年轻人思想、精神状态的差别，她曾经请几个年轻人看卡玛拍的《天安门》，他们对于“八九”一代年轻人眼神中透露出的天下兴亡、匹夫有责的眼神，感到十分陌生和吃惊。今天年轻人遭受现实生存压力，生活、思想空间受到极度挤压，精神状态呈现荒芜景象。不过，在这种大的冷漠气氛中也出现了某种希望，这就是近几年大量志愿者的出现，他们身上体现的承担责任和献身的精神，与“八九”精神有某种程度的相接。<br />
<br />
<br />
维权律师滕彪指出，现在的维权运动是建立在八九民主运动的基础上的，80年代民主运动采取当时的形式，是与那时的政治环境相联系的，而现在的维权运动主要通过个案参与、网络媒体、民间联合等方式，推进人权、法治，从而推动政治体制转型。通过维权抗争的人会越来越多，因为受到侵权的人越来越多，从根本上说是因为极权-后极权体制与人性是敌对的。在互联网时代，维权的方式更多元化。从瓮安事件、杨佳案到三鹿奶粉事件可以看出，现存统治的基础正在逐步丧失，公民社会力量在渐渐成长，政治转型形一定会发生。<br />
<br />
<br />
郭于华指出，想要结束历史悲剧，惟有真实、彻底地记住历史――真实的民主运动。惟有历史的真相是“狼奶”的解毒剂，而在这方面我们依旧任重道远。郭于华还指出，当下抗争的空间是在抗争过程中撑开的；抗争能力是在抗争实践中产生出来的。不能指望统治集团自动地放权，或释放空间。<br />
<br />
<br />
郝建的发言题目为“从陆肆民主运动看中国执政者与百姓的受迫害妄想心态”，他试图借用心理学中的“受迫害妄想症”（Persecution paranoia）来分析这种心理状态对中国政治生活的影响，分析走出这种负面心理状态的必要性和可能性。从陆肆前后中国执政者的种种言行来看，他们中的主要决策者存在着夸大威胁、有偏见地选择证据从而走向偏执反应的情况。受迫害妄想心态在中国百姓中也有表现，而在陆肆之后，过分夸大执政者的能力，过分夸大执政者的非理性状况成为中国百姓的普遍心理氛围。<br />
<br />
<br />
张博树在其论文中指出：“陆肆的血腥结局，显然与当时的中共最高决策者的‘敌对思维’逻辑直接有关。在这个逻辑中，真正的爱国者变成了‘颠覆者’。20年后的今天，党专制的体制已经发生深刻的衰变，‘敌对思维’逻辑对当政者来说已经彻底转化为抗拒宪政民主、抗拒人类普世文明、维护党专制体制的手段。然而，这些年的一个重大变化是，中国民间民主力量已经开始挣脱原来束缚自己的‘敌对思维’传统，我们一方面主张现存体制是没有出路的，它最终将被真正的宪政民主制度所取代；另一方面，我们不把当政者视为‘敌人’，而是人们顺应历史大势，成为改革的促进者而不再是阻碍者。这是一个更高的道德基点，体现了完全不同的政治思维和政治文化。”<br />
<br />
<br />
在会议自由讨论的环节中，针对“以流血换取稳定”的说法以及陆肆镇压与经济发展之间的关系，大家展开了认真严肃的讨论。对于这些为开枪镇压辩护的说法，与会者不予认同，认为这样的逻辑是拿别人的生命为代价换取经济发展，实际上目前中国社会严重贫富不均，充满各种危机。围绕“敌对思维”的提法，研讨会进一步展开了有关民间社会与政府之间关系的讨论。<br />
<br />
<br />
