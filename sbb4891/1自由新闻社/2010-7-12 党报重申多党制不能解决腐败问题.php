													<h2>党报重申多党制不能解决腐败问题</h2>
						
						
																											<div id="postmessage_734983" class="t_msgfont">　　一棵参天大树，若任蛀虫繁衍啃咬，最终必定逐渐枯萎；一个国家或政党，若任腐败毒瘤蔓延，最终也难免趋于衰亡。只有坚决反对腐败，才能保持党和国家肌体的健康，巩固党的执政地位，促进国家的稳定与发展。<br />
<br />
　　近年来，随着一桩桩大案要案被查处，一个个腐败分子被绳之以法，广大群众无不拍手称快，调查显示人民群众对反腐败工作的总体满意度在提升；但在现实生活中，腐败仍然是广大干部群众关注的热点问题。那么，究竟应当怎么看当前腐败现象？如何认识我国反腐败斗争的形势？<br />
<br />
　　● 为何说反腐败不是“雷声大雨点小”？<br />
<br />
　　“反腐败像雷阵雨，雷声大，雨点小，刮一阵风就拉倒。”这是在实际生活中，有的人对当前反腐败形势的一个比喻，认为反腐败斗争“光说不练”，实际成效不明显。那么，反腐败真的是“雷声大雨点小”吗？其实，这种看法并不符合事实。<br />
<br />
　　从一定意义上讲，“雷声大”体现了我们党反腐败的决心大和声势大。这些年来，党中央每年都对反腐败工作进行专门部署，国务院每年召开廉政工作会议，制定了一个个专门文件，出台了一系列重大举措，始终保持反腐败的高压态势。<br />
<br />
　　但用“雨点小”来形容反腐败成效却并不符合实际。事实上，我们党不但高度重视反腐败，而且是雷厉风行、坚决打击，不但是“雷声大”，而且“雨点也大”。<br />
<br />
　　一是“真打”，对腐败分子动真格。反腐败没有停留在口头上，而是落实到行动中。2009年，全国纪检监察机关共立案134504件，结案132808件，给予党纪政纪处分138708人，涉嫌犯罪被移送司法机关处理5366人，还对7036名领导干部进行了问责。深入开展治理商业贿赂专项工作，共查办商业贿赂案件15548件，涉案金额39.1亿元。扎实开展工程建设领域突出问题专项治理工作，立案3517件，给予党纪政纪处分1521人。<br />
<br />
　　二是“狠打”，严查深挖抓“老虎”。不论职务多高，只要搞腐败，就一查到底，绝不姑息。2009年查处了王益、皮黔生、黄松有、陈绍基、王华元等一批大案要案，正在立案侦查的还有许宗衡、李堂堂、黄瑶、宋勇、康日新、张春江等。“经济有特区，反腐无特区”，深圳市原市长许宗衡被抓时一位网友的留言，正说明了这一事实。<br />
<br />
　　三是“善打”，工作水平不断提高。坚持从实际出发，认真总结经验，不断推进工作创新，推动反腐败工作从着力治标、侧重遏制，到标本兼治、综合治理、逐步加大治本力度，再到标本兼治、综合治理、惩防并举、注重预防的转变，努力把改革的推动力、教育的说服力、制度的约束力、监督的制衡力、惩治的威慑力有机结合，形成了反腐倡廉建设的合力，增强了反腐倡廉工作的科学性。<br />
<br />
　　● 为何说“腐败越反越多”是个认识误区？<br />
<br />
　　如果说，“雷声大雨点小”是对反腐败动真格产生质疑的话，那么，“腐败越反越多”的说法则是对反腐败前景表示悲观。腐败真的是越反越多吗？<br />
<br />
　　事实并非如此。如果站在一个客观公正的角度来观察，我国的反腐败形势正在呈现逐步深入、健康发展的态势。这一点有大量事实可以证明。<br />
<br />
　　从量化分析来看， 党的十六大以来， 全国每年受党纪处分人数约占党员总数的比例总的趋势是下降的。2003年以来，全国检察机关立案侦查的职务犯罪数量以年均5%左右的速度下降。<br />
<br />
.........................<br />
　　从民意调查数据来看，国家统计局连续多年的调查显示，2003―2009年，群众对反腐败工作成效的满意度平稳上升，从51.9%提高到69.2%，群众认为消极腐败现象得到不同程度遏制的比例，从68.1%上升到82.6%。这说明反腐败工作得到人民群众的认可。<br />
<br />
　　从国际社会评价来看，这几年不少国际人士对我国反腐败成效给予了积极评价。一位国际反腐专家曾说，中国反腐败成绩是“足以同在中国这样一个世界上人口最多的国家解决温饱问题、极大地消除贫困相提并论的一个巨大贡献”。<br />
<br />
　　这些情况从不同侧面，反映了反腐倡廉工作取得的有目共睹的成绩。当然，我们也应清醒地看到，当前腐败消极现象仍然比较严重，腐败活动向一些关键领域、社会领域扩散，高中级干部违纪违法现象严重，一些腐败分子集政治蜕变、经济腐败、生活腐化于一身，案件类型多样化，作案手段日趋复杂，呈现隐蔽化、智能化的特点。但是，这绝不是所谓反腐败不力造成的结果，而是有其深层次原因的，是由多个方面因素共同作用的结果。当前，我国正处于社会大转型过程中。历史经验表明，在这一时期，腐败现象易发多发具有一定的普遍性，主要是因为：<br />
<br />
　　利益诱惑。转型过程中，在规则不健全的情况下，多元化的利益主体为了追求利益最大化，会使用各种手段来腐蚀干部。同时，社会和其他个人财富增长，对于一些理想信念不坚定的领导干部，具有很大的诱惑力，导致他们心理失衡，于是想用权捞钱。这种内外因素构成的利益诱惑，是诱发腐败的重要因素。<br />
<br />
　　体制漏洞。转型过程中，旧体制已经打破，新体制尚未健全，留下许多漏洞，给腐败现象滋生蔓延留下了很多空间。实践已经证明，凡是双轨制并存的地方，腐败现象就相对严重。当前，因为争取投资、项目、资源、优惠政策等而发生的贿赂，正是体制不完善的结果。<br />
<br />
　　监督不力。转型过程中，权力监控制约机制旧的可能失效了，但新的还未及时建立健全起来。权力监控的缺位和弱化，为权力的滥用、腐败的滋生留下空隙。<br />
<br />
　　道德因素。转型过程中，在窗户打开、新鲜空气进来的同时，苍蝇蚊子也一并进来，资本主义的拜金主义、享乐主义、极端个人主义，封建主义的家长制、裙带风、帮派风，或乘虚而入，或死灰复燃。一些干部不顾廉耻，不讲人格，随心所欲，从而跌入腐败的深渊。<br />
<br />
　　因而，正确认识反腐败斗争现状，要坚持用全面、客观的眼光来分析，既要看到我国反腐败工作取得的成效，又要对当前严峻的反腐败形势保持清醒的认识。<br />
<br />
　　观点声音<br />
<br />
　　腐败是人民的大敌，是党的大敌，也是国家的大敌。<br />
<br />
　　反复发生的问题要从规律上找原因，普遍发生的问题要从体制机制上找原因。只有从制度上构建遏制腐败蔓延的堤防，才能有效防治腐败。<br />
<br />
　　腐败是祸事前因，幸福是勤廉后果。<br />
<br />
　　阳光下的权力，是腐败的克星；暗箱中的权力，是犯罪的温床。<br />
<br />
