　　● 如何推进权力在阳光下运行？<br />
<br />
　　2010年全国两会闭幕后不久，中央很多部门相继在各自网站公布了今年部门预算。中央部门“晒”预算之举，引起了公众的热议，让权力在阳光下运行又一次成为人们关注的焦点。<br />
<br />
　　“不受监督的权力必然导致腐败，绝对权力导致绝对腐败。”能否有效预防腐败，关键是加强对权力运行的制约和监督，让权力在阳光下运行。<br />
<br />
　　一般认为，腐败是公职人员出于私人目的而滥用公共权力的行为，通俗地讲就是“以权谋私”。因而，有人把腐败形容为权力的“影子”，认为哪里有权力，哪里就有腐败的可能性。政治学研究告诉我们，只有加强对权力的制约和监督，才能防止权力滥用和有效遏制腐败。而制约和监督权力的前提是公开透明。要确保权力的合理运行，降低腐败发生的几率，就要让权力 “晒”在阳光之下。正如人们常说的“腐败是腐蚀剂，而阳光是最好的防腐剂”。<br />
<br />
　　网友提问<br />
<br />
　　网友：有人说西方多党制是解决腐败的“灵丹妙药”，对此应怎么看？<br />
<br />
　　答：其实，回顾西方政治发展，多党制自建立起就有腐败相伴而行。20世纪以来，虽然在制度、道德等层面作了规定，但并没有杜绝政党与当政者以权谋私行为。20世纪90年代以来，西方多数国家主流政党都曾陷入腐败丑闻。如1992 年意大利“米兰贿赂丑闻”涉及2000多名政要，2006年美国“游说门事件”牵扯国会议员达296名。<br />
<br />
　　一些发展中国家在推行多党制后，腐败现象不仅没有得到解决，有的甚至更为严重。在有关国际组织提出的2008年世界上最腐败的10个国家与地区中，9个实行多党制。事实表明，西方多党制本身并没有，也难以解决腐败问题，不是也不可能是防治和有效解决腐败问题的“灵丹妙药”。<br />
.............................................................................<br />
<br />
　　当前， 推进反腐倡廉建设，必须坚持权力配置科学、界限明确、行使依法、运行公开，把制约和监督权力的要求落实到权力结构和运行机制的各个环节，逐步推进权力在阳光下运行。<br />
<br />
　　核心是推进权力运行公开。2008年5月《中华人民共和国政府信息公开条例》的颁布，规范了政府的行政行为，对于预防腐败起到了积极作用。应健全权力运行监控机制，加强对权力行使的规范和限制，为权力运行设置“边界”，确保权力既高效运转又正确行使。按照“公开是制度，保密是例外”的要求，深化党务公开、政务公开，完善各类公开办事制度，扩大公开的范围和层次，凡涉及群众切身利益的重大决策都要向社会公开。<br />
<br />
　　重点是加强对领导干部监督。领导干部手中权力大、遇到的诱惑多，如果腐败，造成的危害也更严重。应切实加强对领导干部的监督，严格执行述职述廉、诫勉谈话等制度，推行党政领导干部问责制和廉政承诺制、行政执法责任制，完善经济责任审计，实现事前、事中、事后的全方位、全过程监督。认真落实报告个人有关事项的规定，如实报告住房、投资和配偶子女移居国(境)外、从业等事项。健全干部选拔任用监督机制和干部选拔任用责任追究制度，加强对干部选拔任用提名、考察、决定等环节的监督。<br />
<br />
　　关键是发挥群众的监督作用。人民群众是反腐倡廉的主要依靠力量。近年来全国检察机关立案侦查案件中，70%来自群众举报，许多大案要案的侦破也离不开群众参与。应充分调动群众的积极性主动性，提高群众的监督意识和水平，完善举报人和证人保护制度，确保人民群众的监督权利得到行使。继续拓展群众监督的渠道，创造能够监督、方便监督的平台和条件。重视舆论监督和网络监督，健全网络举报和受理机制，提高举报网站受理案件的办理质量。<br />
<br />
　　● 如何提高反腐倡廉制度执行力？<br />
<br />
　　“天下之事，不难于立法，而难于法之必行。”制定好的反腐倡廉制度不容易，把这些制度落实好则更难。反腐倡廉工作的成效，取决于制度的制定，更取决于制度的执行。<br />
<br />
　　这些年来，反腐倡廉制度建设领域不断拓宽，但是腐败案件还是居高不下，出现了涉案金额大、案犯职位高、作案手段多样化等新的情况。可以说，制度制定初衷与制度执行效果存在距离。究其原因，其中很重要的因素，就是一些制度没有得到有效执行。<br />
<br />
　　制度的生命力在于执行力。好制度不执行，等于没有制度。没有执行力的制度，往往流于形式，成为摆设。只有下决心、下力气狠抓执行力，才能提高反腐倡廉制度的效用，才能打开反腐败工作新局面。<br />
<br />
　　建立责任制。应建立健全保障制度落实的工作机制，完善保障制度执行的程序性规定和违反制度的惩戒性规定，对制度落实的各项措施进行责任分解，明确责任部门和责任人，明确落实时限和阶段性要求。领导干部特别是党政“一把手”要带头执行和维护制度，坚决同一切违反制度的现象作斗争，维护制度的严肃性和权威性。<br />
<br />
　　增强制度意识。制度的精神如果没有深入到党员、干部头脑中，再多再好的制度也只是“耳边风”。要把制度作为反腐倡廉宣传教育和廉政文化建设的重要内容，使广大党员、干部熟知制度内容、领会制度精神，不断增强制度意识、纪律意识、法治意识，牢固树立严格按制度办事的观念，养成自觉执行制度的习惯，把制度转化为党员、干部的行为准则、自觉行动。<br />
<br />
　　加强督促检查。“徒法不足以自行。”制度能否落实，关键在于监督检查。应建立制度本身的监督执行机制，建立健全制度执行问责机制，每项制度都要明确监督执行的责任部门，使制度执行的监督责任无可推卸，对执行制度不力的坚决追究责任。完善制度执行的监督机制，整合监督资源，注重发挥各类监督主体的作用。<br />
<br />
　　加大查处力度。提高制度执行力，必须切实查处违反制度的行为，使制度真正成为不可触犯的“火炉”。对有令不行、有禁不止、随意变通、恶意规避等严重违反制度的行为，要发现一起、查处一起，决不姑息迁就。<br />
<br />
　　“九层之台，起于垒土。”只要我们一步一个脚印，积小胜为大胜，就一定能够切除腐败这个“政治之癌”，建设一个河清海晏、官清民安、邦兴国盛的社会主义和谐社会。<br />
