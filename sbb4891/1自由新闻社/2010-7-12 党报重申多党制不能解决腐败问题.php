<br />
　　● 如何织就一张惩治和预防腐败的“天网”？<br />
<br />
　　中国有句古话：“天网恢恢，疏而不漏。”比喻作恶的人最终逃脱不了国法的惩治。当前依然严峻的反腐败形势，要求我们必须编织一张疏而不漏的反腐败“天网”，逐步铲除腐败现象滋生的土壤，让腐败分子无处藏身，让腐败行为难逃惩罚。<br />
<br />
　　腐败作为一种社会现象，其发生和发展必然遵循一定的规律。急则治标，缓则治本。改革开放以后，针对一度来势很猛的腐败现象，我们党出重拳出快拳，严厉打击腐败分子。随着反腐败斗争的发展，我们党对反腐倡廉工作的特点和规律认识不断深化。如果只抓惩治腐败，可能是一时见效，但往往查不胜查，纠而复生。要从根本上解决腐败问题，必须把防治腐败作为一项系统工程来抓，坚持标本兼治，不断加大源头治理力度。党的十六大以来，以胡锦涛同志为总书记的党中央明确提出，要坚持标本兼治、综合治理、惩防并举、注重预防的方针，建立健全惩治和预防腐败体系，并印发《建立健全教育、制度、监督并重的惩治和预防腐败体系实施纲要》和《建立健全惩治和预防腐败体系2008―2012年工作规划》，勾画了我国惩治和预防腐败“天网”的蓝图及阶段性目标任务。<br />
<br />
　　建立健全惩治和预防腐败体系，是党中央在深入总结历史经验、科学判断形势基础上作出的重大战略决策。这个体系是一张宽广的“天网”，它既包括从严惩治的措施，又包括着力治本的内容，涵盖了党风廉政建设和反腐败斗争的主要任务；这个体系是一张严密的“天网”，它把教育、制度、监督、改革、纠风、惩治统一起来，打出“组合拳”, 使“不想贪、不能贪、不敢贪”形成一个有机整体。可以说，推进惩治和预防腐败体系建设，抓住了党风廉政建设和反腐败斗争的关键，是在发展社会主义市场经济条件下更好地防治腐败的必由之路。<br />
<br />
　　近年来，惩治和预防腐败体系建设扎实推进，在思想教育、法律法规、权力监控、源头治理等方面取得重要进展，进一步形成了全党动手抓反腐倡廉建设的局面。当前，加强新形势下反腐倡廉工作，必须加快推进惩治和预防腐败体系建设，着力抓好反腐倡廉制度建设，建立起科学严密完备管用的反腐倡廉制度体系。<br />
<br />
　　完善教育制度。要把近年来经过实践证明是可行的、有效的教育方法以制度形式固定下来，形成工作常态。以理想信念教育为基础，以领导干部教育为重点，完善示范教育、警示教育、岗位廉政教育以及基层党员轮训等制度，使党员干部筑牢廉洁从政、拒腐防变的思想防线。<br />
<br />
　　完善监督制度。积极探索加强监督的有效途径，建立健全决策权、执行权、监督权既相互制约又相互协调的权力结构和运行机制，保证权力依法运行。改革和完善党内监督体制，健全权力运行监控机制，完善党内决策监督机制，落实重大决策报告制度，健全信访联席会议制度，提高监督权威性和有效性。<br />
<br />
　　完善预防制度。从重点领域、重点部门、重点环节入手，深入查找容易诱发腐败问题的部位和环节，构筑起制度防线。推进廉政风险防控机制建设，形成信息共享和腐败预警机制，建立健全防止利益冲突制度，从制度上更好地发挥市场配置资源的基础性作用，有效排查和化解廉政风险。<br />
<br />
　　完善惩治制度。遏制腐败，惩治这一手任何时候都不能放松。要分析案件查处中的难点问题尤其是制度缺陷，建立健全腐败案件及时揭露、发现、查处机制。加强反腐败立法，修订完善惩治腐败的法律法规，建立腐败现象易发多发领域调查分析和专项治理制度，健全查处案件协调机制，完善案件线索通报移送机制，始终保持惩治腐败的高压态势。<br />
