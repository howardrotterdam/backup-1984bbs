<br />
　　关于牢狱生活：<br />
<br />
　　“最开心的是晚上看《新闻联播》”<br />
<br />
　　节目一开始，满文军就坦白了14天的牢狱生活的感受，<strong>“回忆那14天的光阴，度过得还挺充实的。每天最开心的时候，就是晚上看《新闻联播》。随后再看《保卫延安》</strong>，特别有感受，因为主题曲是我的一个好朋友戴玉强唱的，只有那个时候，我才能大声跟着他唱歌……”在拘留的14天，满文军每天都透过铁窗看着外面的马路和人，“那个时候你会发现自由比什么都可贵。”<br />
<br />
　　对于涉毒事件，满文军也大胆吐露了自己的心声，“在看守所有位民警这样告诉我，每个人的人生都像走路一样，绊倒了，你之后要怎么做？有的人可能一笑而过，但满文军你要把后面的路铺平，不要让以后再跌倒。我希望可以通过我这样一个事情，来警醒世人、警醒年轻人。我觉得无论是抱着一个怎样的心态，都不要碰毒品。今天回过头来想想，我如果可以作为一个反面教材来教育大家的话，我觉得我可以接受。”<br />
<br />
　　关于老婆孩子：<br />
<br />
　　“与儿子变亲密，愿替妻子服刑”<br />
<br />
　　经历了涉毒案之后，满文军坦言自己和儿子的关系变得更亲密了。“以前我的儿子其实跟我不是特别的亲，她跟妈妈特别的好。现在家庭发生巨大变化之后，我跟他相依为命，每天他写完作业我俩都会散步一大圈，我也会陪我儿子完成功课，他有什么和同学吵架也会告诉我。每个周五他也都会跑来问我，‘爸爸我能跟你睡一个床吗？’让我真正觉得我和他的关系变得更加亲密了。”<br />
<br />
　　在北京朝阳法院审理满文军夫妇涉毒案的时候，满文军的妻子李俐面对媒体，曾经表示“不要叫我满文军的妻子”。对此，满文军表示他能体谅妻子为什么这么做，“我知道她是为我好，想把事情担下来……我们的婚姻、爱情、家庭没有问题，这是我和太太共同有的感觉。”同时，对于很多媒体不实的报道，满文军也笃定地表示，“我们永远都不会分开，更不会因为这件事情彼此怨恨对方……我也想通过这个节目告诉大家，有些报道是失实的，满文军的婚姻家庭都是没有问题的，满文军的家庭，都会越来越好、越来越幸福。”<br />
<br />
　　当主持人问满文军会不会替妻子服这一年刑的时候？满文军肯定地回答：“会！”因为在他眼里，现在对家庭来讲，儿子更需要的是母亲，“我更希望他有妈妈来陪他，因为看守所里的生活，我比妻子更加能适应。”而主持人透露在他们去看守所采访李俐的时候，李俐表示：“通过这件事情，我也更爱我丈夫了。”短短的几个字，惹得满文军激动不已、热泪盈眶。<br />
<br />
　　关于未来发展<br />
<br />
　　“远离毒品，我满文军会活过来”<br />
<br />
　　对于未来，满文军也有自己的看法，“其实未来是什么样的，我完全没有预期，这件事情对我整个人生的打击是非常沉重的，但我希望不要被这件事情击倒，我自己可以重新站起来。”<br />
<br />
　　节目现场，主持人尖锐地提出，满文军现在选择接受媒体采访，是不是在进行某种炒作，对此，满文军公司老板宋柯表示否认，满文军也表示现在对他来讲生活已经完全改变了，即便是以后永远不再登台唱歌，对他来说也算不了什么，因为通过这件事，他已经意识到生活中最重要的东西是什么。<br />
<br />
　　节目最后，满文军痛定思痛地面对观众表达了远离毒品的决心，“这件事对我的打击太大了，远离毒品，让自己健健康康的生活，我发誓自己再也不会沾那样的东西，我满文军会重新站起来面对一切，我满文军会活过来。”</div>

							
							
							
							
