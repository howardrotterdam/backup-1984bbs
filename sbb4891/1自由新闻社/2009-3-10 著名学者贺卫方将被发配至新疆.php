北大教授贺卫方今将赴新疆石河子大学支教<br />
2009-03-11 05:28:08　来源: 东方早报(上海)　跟帖 2 条 <br />
贺卫方<br />
贺卫方今赴新疆石河子大学支教两年<br />
<br />
北京大学法学院知名教授贺卫方昨日向早报表示，他将于今日离开北京，前往新疆石河子大学支教两年。对于此次被派到新疆，他自称“也是个不错的选择”。<br />
<br />
去年，浙江大学曾准备引进贺卫方，但在9月份开学前不久，又以“有压力”为由中止了这次引进。这成为坊间猜测贺卫方赴新疆的原因之一，也有说是因为贺平时的一些大胆言论惹恼了校方。<br />
<br />
“确实上下都有压力”<br />
<br />
“是去新疆，”贺卫方昨晚向早报记者证实道，“这是北大和石河子大学对口合作的一个项目，去的时间为两年。 <br />
<br />
”贺卫方此次只身赴新疆，家人不会一起去，“劳动人事关系也不动”。<br />
<br />
对此次调动是不是有外界的压力因素，贺卫方表示“确实上下都有压力”，至于是什么压力，他不愿多讲。那么，此次调动是不是符合自己的愿意？贺卫方解释道：“可以吧！那里也不错，去体验一下生活，了解一下边疆的风情。”<br />
<br />
记者询问“是不是因为平时的一些‘大胆’言论及此前准备调往浙江大学未成一事，才有了这样的一个外派安排？”贺卫方想了一会，回应说“也没有”。<br />
<br />
去年贺卫方曾就浙江大学引进一事不成后又回到北京大学一事向早报记者表示，“北大还算是宽容”。不过，对于贺的工作调动，有网友认为校方不够宽容而表示“可悲”，也有人评论“有流放的味道”。<br />
<br />
“争取做个边塞诗人”<br />
<br />
一位名为宋振江的律师在其博客中称其所在事务所全体合伙人昨专程赴京为贺饯行，“因其所往乃戈壁大漠，自古为流放充军之地，祝酒词是‘劝君更尽一杯酒’，马上有人接‘天下谁人不识君’。把饯行酒，直喝成了‘赛诗会’……兴之所至，卫方君表示要在边疆学写诗，争取做个边塞诗人。”<br />
<br />
石河子大学是“211工程”重点建设高校，教育部指定北京大学、华东理工大学对口支援该大学。 (本文来源：东方早报 作者：简光洲) <br />
