													<h2>英外交官：三峡大坝面临严峻问题</h2>
						
						
																											<div id="postmessage_667240" class="t_msgfont">VOA 中文网&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;记者: 海彦 | 华盛顿&nbsp;&nbsp;2010年 6月 08日<br />
<br />
英国一位长期关注中国三峡大坝的退休外交官称，三峡大坝出现裂缝，大坝至重庆间库区有近万处危险点，30万人受波及需要迁移。<br />
<br />
<img src="http://i45.tinypic.com/14wrl13.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
三峡大坝 开闸放水 (资料照片)&nbsp;&nbsp;AP<br />
<br />
*讨论三峡大坝危害*<br />
<br />
曾长期外驻中国的英国退休外交官提姆.科拉德星期一在英国每日电讯报发表博客文章，称三峡大坝至重庆间的库区时常出现山体滑坡，而且生态环境恶化，大量水藻累积。科拉德说，已经确定有近万处危险点，但是许多居住在附近的居民因为缺乏资金而无法迁移。科拉德表示，三峡工程已经迫使140万人移民，而目前看来还要有30万居民需要迁移。<br />
<br />
科拉德在文章中说，三峡大坝是前总理李鹏特别倾心的项目，上马的政治动机不亚于经济效益的考虑，工程建设甚至在没有经过按照中国标准的适当论证后强行通过。<br />
<br />
科拉德还说，三峡大坝的这些问题不得不由一些官方媒体披露出来，这是因为三峡大坝在造成实实在在的危害。当然中国当局非常谨慎地避免引起大规模公众恐慌，到目前为止，基本上可以阻止集体抗议，地方上的抱怨也能压制下去。<br />
<br />
科拉德表示，与此同时，当局看上去也没有完全封锁消息的意愿，而胡锦涛一直努力不和三峡大坝工程扯上干系，不愿为李鹏去冒险，做火中取栗的事情。<br />
<br />
*三峡大坝代价巨大*<br />
<br />
<img src="http://i46.tinypic.com/vfe4jn.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
一男子在注视着工人们在加固长江河岸 (资料照片)&nbsp;&nbsp;AP<br />
<br />
中国长江专家、四川地矿局区域地质调查队工程师范晓在接受记者采访时表示，尽管他不能确定像科拉德所说的有万处危险点，但目前官方确认的应该在5、6千处以上。<br />
<br />
他说：“他所谓的危险地点主要是指地质灾害，主要包括滑坡、崩塌、泥石流。中国的相关部门最近几年也对三峡库区做了一些比较系统的调查。据国土部的勘察，现在比较确定的大概是在5千到6千处以上这些明确的地质灾害地或者是潜在的。政府现在安排了专门对地质灾害的治理，现在已经在做第 3期了，总投资到现在已经超过近两百个亿。以后还要不断地做下去。所以，现在整个来说，三峡库区的地质危害治理还是一个很严峻的问题。”<br />
<br />
长江专家范晓表示，除了地质灾害，三峡大坝工程对生态环境和当地居民的影响都是巨大的，因此，他认为，至少到目前为止三峡大坝弊大于利。<br />
<br />
他说：“按照我的观点，它肯定是弊大于利的。除了地质灾害，还有就是对生态环境的影响。在没有三峡大坝之前，长江的水是流动相对较快，它是有一定的自我净化的能力。建了水库以后，它的流速变缓，导致它很多污染物质在水中不断地累积。水库两旁有许多支流汇入长江，主要是在这些支流的下游出现水质的恶化。许多地方的水质现在是在劣5类，最差的。由于水质变坏，出现大量的包括水藻的繁殖，主要是绿藻和蓝藻。从03年到现在，它的趋势一直是在扩大。现在在靠近三峡大坝的地方也出现了水华的现象。还有就是大坝修建以后对水生生物的影响是很大的，主要是鱼类，这是最直接的影响。”<br />
<br />
范晓工程师表示，三峡大坝工程的移民问题造成大批人生活贫困化，是需要关注的社会问题。<br />
<br />
他说：“还一个问题就是移民嘛，移民问题可能更严重。水库蓄水以后淹没的整个都是河谷地区，最富足的地方。迁移一个是迁至外省，一个就是就地后靠安置，要迁移到海拔相对比较高的山坡上。一个是它耕地非常少，而且比较贫瘠。它的生活条件或者生存条件和原来的居住的地方都有很大差距，带来一个很大的问题，就是贫困化，包括迁移到外省的。所以它社会问题带来一个后续需要解决的问题。无论是地质灾害，还是生态环境，特别是生态环境，有些它的代价很难现在来估计的，包括社会移民问题。而且这些后续的所要付出的代价，它有些是很难逆转的。”<br />
<br />
有关三峡大坝弊与利的争执一直就没有间断过。以敢讲真话、仗义执言而在学界独树一帜、被其学生称为代表中国水利界良心的已故清华大学水利系教授、知名水利工程专家黄万里，曾孤军论战兴建黄河三门峡水电站的弊端，并因此被打成右派22年，但是事实证明黄万里是对的，三门峡水利工程成为水害工程。作为黄炎培之子的黄万里，曾6次上书国家领导人反对在长江上兴建三峡大坝。黄万里预言大坝终将被炸掉。 90岁高龄的黄万里先生在2001年病逝前曾在昏迷中惊呼“三峡千万不能上”。<br />
