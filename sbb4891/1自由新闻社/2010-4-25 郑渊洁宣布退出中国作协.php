<br />
　　著名儿童文学作家，“童话大王”郑渊洁25日在其新浪博客上发表文章称，依据《中国作家协会章程》第20条，“我宣布从即日起退出中国作家协会。”<br />
<br />
　　日前向玉树灾区捐款100万元的郑渊洁在博文中称，青海玉树地震发生后，给灾区人民生命财产造成巨大损失，但震后两天的4月16日，中国作家协会全国委员会委员、北京作家协会副主席曹文轩却到山东青岛的小学推销自己的图书(作家到小学推销自己的书本身就违反《中华人民共和国义务教育法》第25条)。<br />
<br />
　　郑渊洁认为，“曹文轩身为中国作家协会全国委员会委员和北京作家协会副主席，在玉树地震发生后对灾区人民表现的冷漠，令人失望。我不能与如此‘中国作家协会全国委员会委员’为伍。”<br />
<br />
　　郑渊洁还在文章中表示，《中国作家协会章程》第30条表明，作协的主要经费来源是国家财政拨款。每年这笔数目不菲的钱用于各地作协给200多名驻会专业作家(其中多数是中国作协全国委员会委员)按月开工资和维持作协的日常办公。而作家陆天明曾表示：“恰恰这些领工资的二百名作家，真正写歌功颂德的还极少。这一点大家可以去调查。”<br />
<br />
　　郑渊洁在文章中发问，“难道这200多名每月领取政府发的专门用于写作的工资的驻会专业作家都属于端起碗吃肉放下筷子骂娘一族？获悉中国作协主席团成员陆天明披露的这一重要内幕后，政府还能继续给200多名驻会专业作家发工资？继续给作协财政拨款？”<br />
<br />
　　郑渊洁在文中说，“职责类似于全国人大常委会的中国作协全国委员会的相当数量的成员，应该都在上述人里了，再加上为数不少的根本不懂文学的各地作协的文学官员也是中国作协全国委员会委员(诸如北京作协根本不懂文学没有任何文学作品的副主席李青)，中国作协全国委员会难以促进中国文学的繁荣发展。”<br />
<br />
　　文章最后，郑渊洁宣布：“鉴于此，我决定退出中国作家协会”。<br />
