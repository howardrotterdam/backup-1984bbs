 来源：新华网 2010年09月10日<br />
　荆楚网消息 (楚天都市报) （记者陈俊）宜昌兴山县一村民在自家后山意外发现一个金矿，3天采出金矿4.5吨。当地国土执法与警方联合出击，将金矿全部追回。昨日，该矿井已被封闭。<br />
<br />
　　近日，兴山县国土资源局接到举报，反映水月寺镇龙头坪村有部分村民在自家后山上盗采金矿。当执法人员到达现场时，只发现一处盗采过后10米深的矿洞。<br />
<br />
　　经询问，盗采当事人已将金矿运走。当地派出所接到通知后，立即带领执法人员赶往雾渡河镇，在半路上将载有2.5吨金矿的农用车拦截。<br />
<br />
　　经调查核实，该镇村民郝某前几日在自家后山上意外发现大量金矿，便伙同另外两个村民开始私自采挖，经过3天的洞采共开采出金矿4.5吨。<br />
<br />
　　该县国土执法部门对郝某作出没收非法开采的全部金矿，勒令当事人自行封闭矿井，停止违法开采活动的行政处罚。经批评教育，郝某等人将家里存放的2吨金矿送交国土部门。</div>

							
							
							
							
