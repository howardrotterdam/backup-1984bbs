													<h2>自残</h2>
						
						
																											<div id="postmessage_45064" class="t_msgfont">自残是指人对自身肢体和精神的伤害。一般来说，对精神的伤害难以觉察，因此，如果不特别指明，自残仅仅是指对肢体的伤害。 <br />
<br />
自残的最极端情况就是自杀。 <br />
　　 <br />
自残行为并不少见。每个人都可能产生过自残的念头，只是大多数人没有采取实际行动而已。<br />
　　 <br />
自残的原因： <br />
<br />
1、发展异化 <br />
每个人都存在着创造毁灭欲，GOPLA在解释自残行为的时候是通过发展需求这层面的。 <br />
发展受阻的结果就是另类改变。这是通过毁灭来实现精神能量的释放。 <br />
<br />
2、断绝期望 <br />
很多人都存在期望过高的现象。他们往往会比其他人感受到更多的挫折。(降低期望疗法) <br />
<br />
对于一些他们已经失去信心（可以说绝望）的事情，由于他们存在较好基础，不可避免还是有一些欲求或期望出现，自残往往也为了斩断这些来源。 <br />
<br />
3、焦虑转嫁 <br />
焦虑、紧张、不安、痛苦等得不到化解。自残是一种压力转移的方式。自残是一种不良的发泄方式。一些人会习惯于增加自身肉体的痛苦来减轻精神的痛苦。 <br />
<br />
4、策略 <br />
为了获取其他东西。比如敲诈、获取保险赔付等。 <br />
另一个明显做法就是为了逃避惩罚而进行的逃避性自残。罪犯往往可能自残。特别是小偷，往往这已经成为惯偷早就计划好的行业脱逃手段，甚至团伙规则。在帮派中，为逃避惩罚而自残的现象也比较明显。 <br />
<br />
逃避性自残也体现为为某些不作为或不成功找借口等。舍车保帅，以自残换取生命也是一种策略。 <br />
<br />
5、兴奋点短路 <br />
自虐的快感。一些人会从痛苦中感到放松、刺激或兴奋。他们会习惯性地自虐。自虐中对肉体的伤害就是自残。 <br />
其他痛苦的减轻，如毒瘾发作等。 <br />
<br />
6、自杀未遂 <br />
本意自杀，自杀未遂的肢体伤害现象被认为自残。 <br />
<br />
7、冲动 <br />
冲动时的极端想法也能导致自残。比如赌气，发誓，酒后无法控制自己等。 <br />
<br />
8、外界压力 <br />
许多外界压力会促成自残。这时，自残是被迫的，自残者并不愿意自残。伤害与否取决于外部意志。比如以肢体伤害为标的的赌博的履约等。再如被恶势力强迫自残等。校园暴力中自残现象也不少见。 <br />
