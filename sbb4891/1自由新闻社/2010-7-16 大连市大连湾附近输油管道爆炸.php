													<h2>大连受污染海域430平方公里 清污靠手工方式</h2>
						
						
																											<div id="postmessage_755012" class="t_msgfont">　 (记者涂重航)昨日，国家海洋局海上溢油应急处置前线指挥部发布情况通报表明，大连部分海滩受到大连新港输油管道爆炸事故泄漏原油的污染。<br />
<br />
　　另据国家海洋局透露，大连受污染海域已扩大到430平方公里。<br />
<br />
　　部分浴场受油污影响关闭<br />
<br />
　　16日傍晚发生的大连新港输油管道爆炸事故，经过3天紧急扑救，灭火工作已进入尾声。但事故引发的次生环境灾害，处置难度仍然较大。<br />
<br />
　　昨日大连海域刮起8级大风，中国海监第三支队指挥科科长周端生说，油污现在随风扩散，从大块变成小分块，增加了处理难度。<br />
<br />
　　随着风向的转变，昨日，大连开发区一些浴场受到油污影响已关闭。记者在附近一个浴场看到，海面上零星漂着小块油污，随风可见黑浪。<br />
<br />
　　据中国海监船19日13时30分最新监视结果显示，受污染海域约430平方公里，其中重度污染海域约为12平方公里，一般污染海域约为52平方公里。海洋监测专家表示，漂油对海域的影响不容乐观，清污工作仍在紧张进行。<br />
<br />
　　昨日，国家海洋局大连新港石油储备库管道爆炸事故海上溢油应急处置前线指挥部通报，海监队伍进行岸边巡视，发现金石滩附近海域有条状漂油带，1／3的海滩被石油污染；棒棰岛海水浴场海面和沙滩上都发现大量油污，沙滩的油污基本已被有关人员清理完毕。<br />
<br />
　　清污工作仍靠手工方式<br />
<br />
　　根据大连市政府的意见和安排，辽宁海事局、海洋局调集正在休渔的附近800余艘渔船投入海面清污工作。这些小型渔船发挥灵活性优势，对发现的油污，立即使用吸油毡和消油剂进行清除。<br />
<br />
　　大连市海洋渔业局副局长栾玉u前日介绍，目前海上清污仍依靠“土办法”，除了调集4艘专业清污船外，当地组织800多条渔船加入清污工作。因为缺少消油剂和吸油毡，他们使用草帘子吸附，甚至雇佣民工用瓢舀等手工方式。<br />
<br />
　　大连港全面恢复通航<br />
<br />
　　因风浪较大，大连市海洋与渔业局组织的渔船昨日未投入海上收油作业。<br />
<br />
　　通过近几日进行的海上清污，目前大连港受影响的部分水域通航条件已得到改善，从20日17时起，全面恢复通航。大连新港输油管道爆炸起火事故发生后，为确保船舶通航安全，海事部门对大连港部分港区实施了临时交通管制。<br />
<br />
　　■ 调查<br />
<br />
　　昨日，来自锦州、鞍山等地的消防官兵结束扑救任务，从大连撤回。事故扑救过程中暴露出诸多隐患，其中，起火后，大连保税油库全部断电，油罐没有关闭阀门，大大增加扑救难度。<br />
<br />
　　油库起火操作人员不见踪影<br />
<br />
　　事故区域是大连最大的原油储港口，旁边二三十米是一片化工品存储区，里面的化工品易爆、易燃、重污染。<br />
<br />
　　据介绍，事故发生区域目前建设有185万立方米原油商业储备库项目、300万立方米原油储备基地项目，周边还分布有其他原油、成品油及液体化工储罐，是国家原油、成品油转运的重要基地，每年有将近1500万吨的油品吞吐能力。<br />
<br />
　　而在这么重要的区域内，输油管道与大型油罐之间距离不足十米。而这次首先发生爆炸起火的900毫米输油管道和700毫米输油管道并列，中间不足一米。这两根管道起火后，直接烧垮旁边的十万立方米“103”号油罐。<br />
<br />
　　事故现场数十个罐体，分属于国家储备油库、中石油油库、西太平洋石化公司油库等多家公司，就在大火被扑灭后，消防人员仍分不清事故管道究竟属于哪个公司。<br />
<br />
　　据现场消防员透露，事故发生后，现场操作的8名工作人员全部离开。他们到达现场后，事故油库找不到一名工作人员，后来他们是请邻近的大连港的技术人员才了解到相关油罐的信息。<br />
<br />
　　另外，失火油罐与二甲苯储存区仅隔30米远。二甲苯区共有51个危险化工品罐，易燃易爆剧毒，一旦爆炸，方圆100公里将严重受损。这么危险的区域与原油罐紧邻，增加了安全隐患。<br />
<br />
　　现场断电手动关阀耗10小时<br />
<br />
　　大连市消防特勤二中队指导员桑武是攻坚组组长。他说，16日晚8时许，他带领任务前往火场关闭油罐阀门。但旁边的技术人员称最多半小时即可关阀。<br />
<br />
　　随后他们到达现场后发现，10万立方米超大型油罐阀门是一个类似方向盘大小的圆形阀门。他转了十多分钟，仍然没有丝毫关闭的感觉。他到现场查看，发现阀门螺丝长达1米多高，而技术人员告诉他，每转动80圈才能前进一扣。要关闭一个阀门需要8万圈。<br />
<br />
　　桑武和战友耗费3个小时才关闭一个阀门，而这中间，油罐原油源源不断向火场供油，给现场官兵增加很大扑救难度。<br />
<br />
　　据现场消防员透露，他们了解到，这所油库的阀门基本都没有关过。<br />
<br />
　　辽宁省消防总队副总队长李金华说，他们准备向公安部提出建议，经过这次扑火发现，油库发生火灾后全场断电，而现场的油罐需要专业的“380号电”，在没有电力的情况下，大型油罐阀门很难关上。<br />
<br />
　　在扑火过程中，关闭油罐阀门耗时十个小时。在火灾第二天凌晨4点半，大连火灾指挥部调来专业供电车后，每关一个阀门仅需30秒。<br />
<br />
　　泡沫灭火剂储备不足靠空运<br />
<br />
　　据大连市安监局副局长孙本强透露，在事故发生前，他们就提出建议要尽快实现油库区封闭管理，但是直到如今，距离油库20多米的周围仍分布30多家其他企业。<br />
<br />
　　消防人员在现场发现，罐体间的消防通道过于狭窄，火灾发生后，消防车辆无法展开队形，进出受到影响。在扑救进程中，还出现了泡沫供应不上的局面。<br />
<br />
　　现场一位工作人员说，像这么一个大型油库，事前应该有危险意识，应储备至少三个大型油罐灭火的泡沫。然而，油库自身并没有储备。最后只好依靠空运来解决。<br />
<br />
　　扑救过程中，还出现了水供应不上的局面。最后有关方面下令取用可能对消防装备产生不利影响的海水，才解决了这一问题。<br />
<br />
　　另据海事部门的数据显示，大连港是中国东北最大的油品及液体化工品储转分拨基地，拥有全国最大最先进的30万吨级原油码头，可接卸30万吨超大型油轮，年通过能力5700万吨。但是当地拥有的溢油处理能力无法满足船舶溢油事故的需要，特别是这种特大事故。</div>

							
							
							
							
