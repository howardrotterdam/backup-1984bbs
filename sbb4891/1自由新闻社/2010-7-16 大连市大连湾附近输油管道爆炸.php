													<h2>交通运输部通报称大连油管爆炸属陆上操作责任</h2>
						
						
																											<div id="postmessage_749390" class="t_msgfont">2010-07-19 12:57:59　来源: 中央人民政府网站<br />
<br />
核心提示：交通运输部通报：7月16日18：20时，新加坡太平洋石油公司所属30万吨原油船“宇宙宝石”轮在大连新港中石油原油储备库卸油过程中，由于原油储备罐陆地管线在加催化剂作业时发生爆炸，引起火灾，并导致部分原油泄漏入海。<br />
<br />
中央人民政府网站7月19日报道<br />
<br />
关于切实加强港口油品<br />
<br />
接卸作业安全生产的紧急通知<br />
<br />
交安委明电〔2010〕7号<br />
<br />
各省、自治区、直辖市交通运输厅（局、委），天津、上海市交通运输和港口管理局，长江、珠江航务管理局，中远、中海、中外运长航、招商局集团，部直属海事局、救捞局：<br />
<br />
7月16日18：20时，新加坡太平洋石油公司所属30万吨原油船“宇宙宝石”轮在大连新港中石油原油储备库卸油过程中，由于原油储备罐陆地管线在加催化剂作业时发生爆炸，引起火灾，并导致部分原油泄漏入海。事故发生后，胡锦涛总书记、温家宝总理立即做出重要批示。中共中央政治局委员、国务院副总理张德江代表中共中央、国务院连夜紧急赶到事故现场，指挥灭火救援工作。李盛霖部长等领导立即赶赴现场，组织指挥交通运输系统相关部门紧急开展消防、清污等工作。目前，库区明火已被扑灭，海上油污清除工作正在有序、紧张进行。当前，正值防汛抗台战高温关键时期，为吸取大连“7・16”事故教训，防止再发生类似事故，确保港口油品接卸作业安全，现就有关要求通知如下：<br />
<br />
一、强化港口安全生产工作<br />
<br />
各部门、各单位要充分认识当前防汛抗台战高温期间安全生产形势的严峻性，认真吸取大连事故的教训，做到举一反三、充分准备、有效应对，要进一步落实安全生产“两个”主体责任，加强层级安全生产监督管理，强化预防预控，并要制定有针对性的措施，确保港口油品接卸作业的安全。<br />
<br />
二、强化安全隐患排查治理<br />
<br />
各交通运输企业、特别是港口企业要立即组织对港口油品接卸工作进行拉网式隐患排查，要集中人力、物力和财力加大隐患治理的力度，及时消除事故隐患，保持生产设备设施处于良好的技术状态。对于一时难以消除的重大事故隐患，要实行挂牌督办、限期整改。<br />
<br />
三、强化港口生产作业现场管理<br />
<br />
港口现场作业人员要认真落实岗位责任制，严格执行港口和危化品安全操作规程，注意谨慎操作，做到万无一失；现场管理人员要强化现场巡回检查，及时发现问题，及时果断处置，做到管理无缺陷；企业领导要把安全生产作为当前工作的重点，坚持亲自主抓，坚持带班值守，做到督促落实到位。<br />
<br />
四、强化安全有效监管<br />
<br />
各交通运输主管部门和海事部门要对所有的油品接卸港口码头进行最严格督查，加大安全监管和执法力度，凡不符合安全生产要求的，要坚决停产整顿。要重点强化现场作业的有效监管，对港口油库、输油管道、设施设备以及运输船舶做到安全监管无死角。<br />
<br />
五、强化应急救援准备<br />
<br />
各部门、各单位要借鉴大连“7・16”事故的经验和教训，及时修改完善本部门、本单位相关的应急预案，要加大投入，加强消防、溢油等应急物资的储备，加强应急队伍建设和应急演练，切实提高突发事件的应急处置能力。<br />
<br />
二一年七月十七日<br />
