													<h2>江泽民：压低调子 不要向我脸上贴金</h2>
						
						
																											<div id="postmessage_608488" class="t_msgfont"><strong>《日出江花》出版记:江泽民要求压低调子不贴金</strong><br />
<br />
<a href="http://news.163.com/10/0510/20/66BMU9980001124J.html" target="_blank">http://news.163.com/10/0510/20/66BMU9980001124J.html</a><br />
<br />
2010-05-10 20:20:41　来源: 南方人物周刊(广州)<br />
<br />
核心提示：具有官方背景的江泽民传《日出江花》近日出版。与此前传记不同，《日出江花》一书，江泽民本人做了大量工作。责任编辑陆宗寅表示，在书稿成型后，江泽民发表意见，凡是夸他的用词，都要尽量压缩，“压低调子”“不要向我脸上贴金”。<br />
<br />
南方人物周刊5月10日报道&nbsp;&nbsp;对云南大学外语学院的学生马琴华来说，4月22日，是个难忘的日子。那一天，在上海世博园区云南馆做志愿服务的她，亲手为原中共中央总书记江泽民献上了象征吉祥、健康和平安的香包。<br />
<br />
马琴华记得，江泽民、李鹏、朱F基等老首长们，是在上海市委书记俞正声、市长韩正的陪同下，乘坐电瓶车来到世博会参观的，到云南馆的时候，副馆长康向萍率全体工作人员和文化演出队伍载歌载舞、列队迎接。<br />
<br />
江泽民微笑着，不断向大家挥手致意，当马琴华说“祝您吉祥、平安、健康”时，江亲切地与她握了握手。<br />
<br />
“当时，我遇到了一个难题，江爷爷戴了顶黑色礼帽，我准备把香包给他挂在脖子上时，因为香包的吊带直径较小，帽子大了戴不上去，这时，江爷爷就微笑着取下了帽子。”马琴华说，那一天，她很兴奋。<br />
<br />
两周前，参观外滩的不少游客也见到了这位已经84岁高龄的老人。互联网上流传的一段视频显示：江泽民坐在一辆中巴车上，身穿深色西装，左手按车窗边沿，右手向民众挥动，人群中发出欢呼声。有网友兴奋地留言：“今天看到很多人，还有江泽民同志，哈哈……”<br />
<br />
有心人注意到，世博会前夕，一套名为《日出江花――青年江泽民在上海》(下简称《日出江花》)的丛书，一共4册，开始在上海热销。<br />
<br />
4月24日，该书责任编辑、上海人民出版社的陆宗寅告诉本刊，《日出江花》这套书起印3万册，还没有印刷完，就已经被相关单位预订了两万册，加上要在全国各地铺货，很快又要加印了。<br />
<br />
“官方背景”的出版物<br />
<br />
据本刊了解，早在2007年前后，上海市有关方面已经开始着手筹备这套《日出江花》，前后历时3年多，“采访了近百名老同事、当事人，查阅了大量档案、史料，征集并拍摄了200余幅图片”。<br />
<br />
有关江泽民的传记已出版多部：2005年初，美国人罗伯特・劳伦斯・库恩撰写的《他改变了中国――江泽民传》，引发巨大反响；第二年，一汽集团档案馆组织编写回忆文集《江泽民在一汽的岁月》；同年，上海交通大学编写《江泽民和他的母校――上海交通大学》；2009年，由丁瑞芝等人撰写《江泽民同志在武汉热工所》……<br />
<br />
和上述这些出版物不同，《日出江花》带有强烈的政府背景――编委会主任为上海市委常委、市委秘书长丁薛祥，副主任分别为上海市委宣传部常务副部长宋超和江泽民学弟徐嘉诰，成员包括上海市委办公厅主任马建勋……在4月18日的出版座谈会上，中共上海市委副书记殷一璀出席会议并讲话，市委常委、宣传部长杨振武出席座谈会。<br />
<br />
殷一璀说，“这套书的出版，为今天我们继续坚定理想信念、艰苦奋斗、立足前沿、开拓创新提供了重要的思想借鉴和精神动力，为新一代年轻人的成长与学习提供了生动的样本和范例，同时也为党史研究、理论研究提供了重要的参考和依据。”<br />
<br />
该书责编陆宗寅说，据他了解，这套书出版的初衷，是江泽民在上海工作过的一些单位，在整理厂史过程中，感到和江泽民有关的那段历史有出版价值。后来这些想法得到上海市认可，开始着手成立编委会。<br />
<br />
而编委会成员、江泽民曾经工作过的上海电科所党委书记张玉磊的说法是，他们确实有将江泽民同志在电科所工作经历整理出来的想法，但没有力量，后来，“接到上海市委宣传部的通知，需要我们配合。”<br />
<br />
与高规格的编委会对应，《日出江花》的写作团队也很强大，来自上海滩三大报――《解放日报》、《文汇报》、《新民晚报》的6位作者，都是所在报纸的资深记者，如《文汇报》高级记者郑重，《新民晚报》高级记者唐宁等，都著作等身。<br />
<br />
“不要向我脸上贴金”<br />
<br />
与撰写《江泽民传》的库恩声称与江泽民只有几面之缘不同，《日出江花》一书，江泽民本人做了大量工作――亲自参与了当年有关细节的回忆、校正，而且亲自联系若干采访对象。<br />
<br />
<br />
“江泽民同志会告诉我们，这个事情，你采访谁比较合适，然后亲自提供电话，帮忙联系。”《日出江花》的一位写作者说，江泽民同志曾告诉她，“记者啊，就要打破砂锅问到底，要如实写，不要向我脸上贴金。”<br />
<br />
责任编辑陆宗寅表示：在书稿成型后，江泽民发表意见，凡是夸他的用词，都要尽量压缩，“压低调子”。<br />
<br />
通读全书，可以发现，和江泽民共事过的绝大多数老人，后来都和江泽民有或多或少的联系，而江的接见，也成为他们人生中宝贵的回忆。<br />
<br />
比如上海电科所原动力科科长蒋鸿定，1993年的时候，去过中南海江泽民同志的家，江设家宴款待蒋，并屡屡劝酒，让蒋感动异常――“我当时举着酒杯啊，又惊又喜，嘴上说着无上荣幸，心里是无比暖和。”<br />
<br />
原上海电科所所长崔镇华的妻子回忆，“江泽民同志也站住不动了，然后彬彬有礼地将手一伸，请我先上车，真的叫受宠若惊啦，外交礼节我不懂，‘总书记请我先上车’这大概是总统待遇吧。”<br />
<br />
让《日出江花》采写团队印象深刻的还有，在采写过程中，他们拿到了新素材，会去和江泽民互相印证。这个阶段，江往往会提出新线索，比如，记得和某某在一起拍过照，和某某一起出过差。<br />
<br />
“江泽民同志记忆力惊人，很多宝贵的照片和线索，就是这么着被挖掘出来的。根据他的思路去找人，很多时候，往往比参与回忆的人还清楚。”<br />
<br />
责任编辑陆宗寅说，书稿成型后，传到江泽民手中，江发现他入党时间搞错了。采写者们说的是江泽民在上海交大入的党，而江本人的说法是，他并不是在交大系统的党组织加入中共的。“经过仔细审核、校正，后来，我们发现，江泽民同志是正确的。”陆宗寅说。<br />
<br />
和汪道涵的“缘分”<br />
<br />
在《日出江花》一书中，还披露了一些以前甚少见到的珍贵史料。<br />
<br />
美国人库恩的《江泽民传》中，讲述了原上海市长汪道涵与江泽民亦师亦友的关系，汪道涵曾经多次在关键时刻推荐江泽民。不过，库恩的著作没有进一步阐释汪道涵为何与相差了10多岁年纪的江泽民，有如此“缘分”。<br />
<br />
《日出江花》一书对这“缘分”背后的故事作了一个交待：“抗日战争爆发后，江上青(江泽民养父)被派往安徽做统战工作，打入国民党安徽省第六行政区任督察专员、公署秘书兼第五战区第五游击区司令部政治部主任，在党内则是‘皖东北特派员’，是皖东北抗日根据地创始人之一……根据江上青的要求，由上海地下党派的一批城市地下党员，被充实在第六行政区所属的县里任党政职务，其中就有汪道涵，被委任为中共嘉山县委书记。江上青又说服盛子瑾，给了汪道涵一纸县长委任状。”<br />
<br />
“江泽民同志和汪道涵的夫人戴锡可也有故人之缘……江上青主持过革命青年训练班，戴锡可即是训练班的学员，江上青则是她的老师。因此，戴锡可对老师的儿子――江泽民同志，总是以大姐姐的身份对他关怀和爱护。”<br />
<br />
<br />
上海交通大学毕业的江泽民，第一份工作――益民食品一厂，在解放后，正好归属汪道涵妻子戴锡可领导，而戴又是汪道涵华东工业部的属下。于是，江泽民和戴锡可、汪道涵一家越来越熟悉，江泽民成了汪道涵家的常客。<br />
<br />
“除了谈工作，也谈其他事情，比如说我们的家庭。”汪道涵告诉采访他的美国人库恩，不管是碰上了充满机遇的挑战，还是碰上痛苦的磨难，江泽民都会向他的妻子倾诉，而他和江泽民也逐渐发展为一种终身不渝的友谊。<br />
<br />
当然，“江泽民同志和汪道涵的友情并不只是革命情缘，他们有着很多个人间的共同话语，他们同为安徽人，又先后为交通大学学生，汪道涵读的是机械专业，江泽民同志读的是电机专业，两人又都是学生运动中的佼佼者，又都是学生时代就参加了中共地下党，再有两人又都好读书……”<br />
<br />
“再没吃过那么好吃的鸭子了”<br />
<br />
《日出江花》一书中，还讲述了江泽民与原一机部第一任部长黄敬的一段交往。<br />
<br />
“1953年，第一机械工业部部长黄敬，为感谢捷克专家许嘉德先生的帮助，特意在前门全聚德烤鸭店设宴犒劳，设计总局局长李如洪和江泽民同志等也出席作陪。入座后，只见门帘一掀，厨师用小车推来一只油光锃亮的烤鸭，当场切出香脆的鸭皮，宴间每道菜肴都以鸭子做食材……”<br />
<br />
这是第一次品尝全聚德烤鸭，而且是和单位最高领导一起，50多年后，江泽民告诉采访者，“再没有吃过那么好吃的鸭子了！”<br />
<br />
1956年，时任长春第一汽车制造厂动力处处长的江泽民，又和黄敬相遇，江泽民清晰地记得黄敬部长与他的座谈，“从晚上7点钟开始一直谈到晚上11点，黄敬部长问得详细而具体，如为全厂供电供热的抽汽式汽轮机的性能、效率等”。<br />
<br />
“黄敬部长在厂调研期间，和苏联专家随便交谈，也参加跳舞，他的舞跳得很好，有时节假日，厂领导陪他去长春附近的净月潭旅游或者狩猎……他会休息，更会工作。他工作时严肃认真，休息时谈笑风生。”<br />
<br />
2009年10月，江泽民带着一封写于57年前的信，去医院看望原外交部长黄华。原来，57年前，时任上海市人民政府外事处处长的黄华和益民工业公司总经理戴锡可直接领导了江泽民来接管英商中国肥皂公司。<br />
<br />
江泽民回忆，接管工作组的成员曾经多次去外事处开会，讨论接管中的各种情况，研究对付英商的具体策略。江泽民说：“在我印象中至少有过3次，我们一起在上海大厦开会研究。”<br />
<br />
时光流转，57年后，当拿着那封发黄的信笺，说起当年上海肥皂厂接管中的往事，躺在病榻上的黄华对着江泽民连连点头，“目光中闪现激动的神色，伸出双手，与江泽民紧紧相握，互致老战友的布尔什维克之礼。”<br />
<br />
念旧情<br />
<br />
在《日出江花》丛书中，一篇署名为曾培炎的文章引人注目。原来，曾任国务院副总理的曾培炎，从清华大学毕业后，从事的第一份工作，就是江泽民任第一副所长的上海电科所。<br />
<br />
在这篇回忆文章中，曾培炎深情地回忆，“江泽民同志是我踏上社会后最早的领导之一，虽然我们之间差距很大，接触也有限，但他的一言一行都给我留下了很深的印象……1997年10月，我陪同江泽民同志在美国进行国事访问，他想看望教过自己的老师顾毓L教授。顾老知道江泽民同志访问日程紧张，要来看他。江泽民同志坚持不让，百忙中抽出时间从华盛顿赶到费城顾老家里。”<br />
<br />
“江泽民同志与交大的老师沈尚贤教授也多年保持联系，一次想去看望，意外得知沈老两天前刚刚去世。江泽民同志很是遗憾，执意去看望慰问了家属。”<br />
<br />
不止是师友，《日出江花》责任编辑陆宗寅说，他编辑书稿，得到最大的感慨是，江泽民同志很念旧情，对那些曾经的同事，他是有机会就会去找他们见面聊聊，或者打电话进行沟通。<br />
<br />
比如，1985年江泽民到上海当市长后，一次到虹桥机场送人，在那短短的空档，他打电话给时任上海电科所所长薛南行：“老薛，我在虹桥机场送客人，想到所里看看，不知是否方便？”<br />
<br />
参加工作之初，在上海北四川路区委工作过的一位名叫凌灿英的同志给江泽民留下了很深的印象。《日出江花》一书披露，几十年后，江泽民还在寻找这位认真、负责的女同志，很费了一番周折，从茫茫人海中找到了分别57年的凌灿英。2009年年底，江邀请凌和老伴去中南海家里叙旧。<br />
<br />
那些没有再和江泽民接触过的一些当年老同事，也常常回忆起和江共事的岁月，那仿佛是个神奇的时空魔方――上海电科所原工程师窦植说，“常常会想起夜校下课后我们(窦植和江泽民)谈谈说说走回家的情景……6分钱一只茶叶蛋，热乎乎、香喷喷，以后好像再没吃过这么香的茶叶蛋了……”<br />
<br />
(本文来源：南方人物周刊 作者：陈磊)</div>

							
							
							
							
