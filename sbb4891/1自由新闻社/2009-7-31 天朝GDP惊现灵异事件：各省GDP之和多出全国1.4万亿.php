																											<div id="postmessage_185761" class="t_msgfont">各省GDP之和多出全国1.4万亿 专家称有重复计算<br />
2009年07月31日 来源：中国经济网 　<br />
[提要] 根据公布的数据，今年全国31个省市自治区公布的上半年GDP之和，比国家统计局发布的上半年国内生产总值约多出1.4万亿元，专家表示，要客现看待这一问题，各省市自治区统计的数据，存在重复计算的问题，现在的关键是要将统计数据“夯实”。&nbsp;&nbsp;<br />
　　各省GDP之和多出全国1.4万亿 湖北专家:有重复计算<br />
<br />
　　记者查阅媒体公开披露的数据发现，全国31个省市自治区公布的上半年GDP之和，比国家统计局发布的上半年国内生产总值约多出1.4万亿元。中南财经政法大学教授叶青表示，要客现看待这一问题，各省市自治区统计的数据，存在重复计算的问题。<br />
<br />
　　7月16日，国家统计局新闻发言人李晓超表示，初步核算，上半年国内生产总值139862亿元，按可比价格计算，同比增长7.1%。 <br />
 <br />
<br />
　　此后，记者陆续查到全国31个省市自治区经济“半年报”。记者发现，31个省市自治区上半年GDP之和为153769.4亿元，较国家统计局公布的139862亿元要多出约1.4万亿元。两者为何会出现如此大的出入？<br />
<br />
　　中南财经政法大学教授叶青表示，要客现看待这一问题，各省市自治区统计的数据，存在重复计算的问题。比如一家大型公司，在各地均有分支机构，公司总部算进去各分公司营收之和。而各地又将各分公司营收纳入统计。因此，经常出现各地GDP总和大于国家统计局发布的数据。<br />
<br />
　　叶青表示，现在关键是要将统计数据“夯实”，出口与消费统计数据不易失真，投资这一部分，统计的数字不能仅看纸面上的，而是要统计有多少投资最终转化为实物工作量。<br />
<br />
　　叶青表示，虽然我国22个省市自治区上半年GDP同比增长超过8％，但各省的情况不能代表全局，目前国家政策的制定，以国家统计局发布的数据作为参考。所以，目前国家刺激经济增长的政策并未见调整。</div>

							
							
							
							
