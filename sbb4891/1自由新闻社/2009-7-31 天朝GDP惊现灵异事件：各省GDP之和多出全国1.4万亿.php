																											<div id="postmessage_188483" class="t_msgfont"><div class="quote"><h5>引用:</h5><blockquote>原帖由 <i>est</i> 于 2009-8-1 01:23 发表 <a href="http://1984bbs.com/redirect.php?goto=findpost&amp;pid=186559&amp;ptid=18395" target="_blank"><img src="http://1984bbs.com/images/common/back.gif" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /></a><br />
1984上脑残也不少<br />
假如一个成都老板今年在广州的公司营业额是500万并且季末把钱带回了成都，很明显两地的gdp都会各自算500万。所以各省gdp总和铁定会超过全国。明白不？<br />
<br />
<br />
<br />
编辑：我只是用最简单的方式的打了个 ... </blockquote></div>您这个例子不对，从经济学定义而言，GDP是一定时期内一个国家或地区所生产出的全部最终产品和提供劳务的市场价值的总值。GDP强调的是地区生产概念，就是说一定是要对应于某个地区生产。您这个例子要分两种情况来分析，第一种是成都的老板在成都是总公司，在广州是分公司，那么这500万是应当计入成都的，从企业合并报表来说。第二种情况就是，成都公司与广州公司不存在隶属关系，只是一个人开了两家公司而已，或者说广州公司是成都公司的子公司的话，那么广州的钱就计入广州GDP而不能计入计入成都GDP。所以要具体分析这种情况。<br />
工作原因，我同地方统计局的人员打过交道。统计工作在中国具有艰巨性和复杂性。如果企业经营者的确如实申报，并且明确总公司和子公司的关系或是总公司与分公司的关系，那么在统计上就不会出错，对于分公司的收入就不会计入地方GDP，而对于子公司则是要记入本地GDP。但是现实生活中有些私营企业主因为一系列原因瞒报这种隶属关系，那么在统计上的确就会出现误差。所以GDP出现误差是正常的，但是这个误差不应当如此之大。因为对于在国民经济中占主导地位的国企和占不小比重的外企而言，这些公司的隶属关系是很明确的，都在当地税务机关和工商局注册备案的，那么就不会出错，出错的更多可能是在民营或是私营经济这一块，但是从绝对比例来讲，这个比重不大。<br />
但是地方的统计工作是一项政绩工程，在统计工作大体上不出错的基础上，各级地方机关都会在数据中加水。加水最猛的是最底层的机关，比如乡镇等机关。这些地方的统计数据是基础数据，而这些地方的官员又是最希望通过政绩获得提升的，因此数据中大量注水。等到了上一级统计机关后，上一级统计机关对下层机关工作心知肚明，他们自己也是算了一个统计数据，于是就与各地数据汇总后的数据进行对比，毫无疑问后者数据更大，他们通常的做法就是对各地的数据进行修正，在工业上削减一点，或是在服务业上削减一点，因为外汇外贸这一块数据很难造假，主要就是通过工业增加值和服务业产值这两个大头进行调整，这些数据调整一个，就要导致一连串数据的重新计算。所以上级机关的统计数据出现漏洞是在所难免的，因为很有可能顾此失彼，因为不知道某一个数据会造成哪些其他数据变动。<br />
每一层上级机关干的事情就是对下级机关的统计数据进行抽水和重新注水。抽水的意思就是对下级机关报上来的带有明显水分的数据进行压缩，把相关数据调低。重新注水是要根据本级领导干部的意图和政绩要求进行有针对性地调整数据，重新夸大。比如市级领导要求服务业产值要上去，那么市级统计局就会将地方统计数据的服务业产值这一块调高，而将工业增加值进行调低，这样从整体上看服务业比重就提升了，这样就实现了上级领导要求的调整产业结构的要求，地方领导的政绩也就提升了。而到了省级机关的时候，省级机关又有省级的调整目标，于是数据要依照省级领导的意图进行重新调整。<br />
这些手法是地方统计局的官员和我们座谈的时候提到的，因此他们自己也承认，统计数据顾此失彼很严重，因为要层层调整。所以每年在出版统计年鉴的时候是最痛苦的，他们要不断调整地方数据来保证相对数据误差最小。只要用心研究就会发现市级统计年鉴和省级统计年鉴中的误差还是很多的，尤其在一些细微数据上。<br />
<br />
所以造成这么大的误差的原因有很多，您说的那个原因是存在的，但是不是主流，不能够成为这么大误差的主要原因，更多地还是各级统计机关在数据处理上的抽水和注水造成的。毕竟，很多工作在天朝进行是很复杂的，实际操作的魅力远远超出书本理论，他们的手法远远超过你我的想象能力。在天朝，一切皆有可能。</div>

							
							
							
							
