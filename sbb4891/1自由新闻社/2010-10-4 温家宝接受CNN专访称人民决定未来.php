温：我的书架上经常摆着的是有关历史的书因为我相信“以史为鉴，可以知兴替”。中外的历史书我都很爱读。在我出差的时候，我一般会随身携带两本书，一本是亚当斯密的道德情操论，另一本是沉思录。我并不是同意这两本书中的所有观点，但是我相信古人的理想和思考会为今人提供思考的食粮。现在有太多自夸自卖的回忆录，我并不喜欢读。我相信一个人应该留给世界的是真相――那些真实的东西，但即使是真实的东西也终将消散。<br />
<br />
Z：最后一个问题，温总理，你的坦诚让我感动。你刚才提到你不管国内党内的阻碍有多大你都要继续推动政治体制改革的决心，你对于腐败和通胀会动摇社会稳定的忧虑，以及对胡耀邦领导能力的赞扬，即使他是一个危险的“自由化”领袖。<br />
你相信中国即将上台的新一代领导人，会和你有共同的远景吗？他们会不断努力深化你正在力推的事业吗？<br />
温：你可能认为这是今天最难回答的问题，但在我看来这很容易。我从两点回答你有关未来的问题。1.正如中国老话说的“长江后浪推前浪，一代新人换旧人”，对中国未来领导人超过他们的前任我深有信心。2.是人民以及人民的力量决定了国家的前途和历史。人民的希望和意愿不会止息，顺之者昌，逆之者亡。谢谢你的专访<br />
Z：这是我的荣幸，谢谢你温总理。<br />
