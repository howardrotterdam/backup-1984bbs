													<h2>9月23日温家宝总理接受CNN专访全文</h2>
						
						
																											<div id="postmessage_855250" class="t_msgfont"><a href="http://dongxi.net/b02Rm" target="_blank">http://dongxi.net/b02Rm</a><br />
译者：老杨 2010年10月04日 21:35<br />
<br />
9月23日CNN，“FAREED ZAKARIA GPS”栏目主持人Zakaria(以下称为Z)专访温家宝（以下称为温）<br />
<br />
Z：你认为当前全球经济形势是稳定，而且充满活力的吗？你是否担心出现所谓的“二次探底”，导致美国经济再一次进入衰退？<br />
温：客观的说，我认为当前全球经济正在复苏，尽管这个复苏的过程有些漫长，充满曲折。不同的人有不同的看法，但是我相信现实给我们好好上了一课。我希望美国经济能够快速的恢复，因为美国经济实力毕竟是世界上最强大的。<br />
奥巴马总统最近的一些经济政策，包括美国出口倍增以及对基础设施大量投资的计划，是解决问题的正确之道。尽管这些政策来得稍晚了一些，但总算及时。<br />
这让我想起了我决定四万亿投资刺激中国经济的时候。当时人们对这一政策有着各式各样的看法，但是现在的经济态势显示我们一揽子刺激计划是有效的，成功的<br />
<br />
Z：你的经济刺激计划，从占GDP的百分比来说，是美国计划的十倍， 真是一个异乎寻常的举动。是否存在这样的担心，如此巨量的投资将在中国的房地产市场产生泡沫？政府投资了这么多钱，是否会带来通货膨胀呢？现在投资计划减弱了，有没有什么影响？政府的钱会不会不够？<br />
温：从你的问题来看，我认为你并没有对我们的刺激计划有一个全面的了解。我们的经济刺激计划有四个重要的组成部分：1.大规模的公共投资，结构性的减税以及基础设施的建设。2.调整和升级产业结构。3.科技创新以及发展有战略意义的新兴产业。4.改进社会保障体系。整个4万亿人民币的投资计划并不是都来自于公共财政，政府只提供其中1.18万亿人民币，余下的会从非公有市场以及金融市场中筹措。刺激计划的实行，保证了中国经济的持续稳定和快速发展。它帮助中国经济能够保持30年来快速发展的好势头，同时也让我们避免了中国现代化进程，由于外部条件剧烈变化而产生大的波动。同时，经济刺激计划为将来中国经济的发展打下了坚实的基础，我们对在你问题中提到的那些挑战保持着高度的警觉。<br />
我从三个方面回答你：第一，中国确实存在通货膨胀的风险，这也是我们提出要小心调整好社会稳定、经济快速发展、经济结构调整和管理通胀预期这一重要任务的原因。这是中国宏观经济调控的核心。我对中国管理通胀预期确实存在担忧，这也是一直以来我着重要妥善管理的地方，因为我认为腐败以及通货膨胀会对我国政权的稳定造成负面的影响。这两个问题都需要广大人民群众对政府的信任和支持。这是我对中国通胀问题的看法。<br />
第二，随着经济刺激计划的实施，地方政府会有一定财政金融上的风险。地方政府有融资工具，也有一定的债务。但这并不是一个经济危机之后才存在的新问题，其实在1980年代中国就已经存在。现在地方政府通过各种融资渠道，累计了7.6万亿人民币的债务。地方一级的这些债务，仍然在我们可以管控的范围内。但是在地方政府层面妥善的管理这些债务是很重要的，以免对公共财政和金融部门带来风险。预算赤字占中国GDP的比例为3%，而中国所有债务占GDP的比例是20%。也就是说，目前仍然在我们可控的范围之内。<br />
第三方面，当前所有的投资必须要有利于我们经济结构的调整，而不是阻碍。这一点有关我们长期的发展前景因此显得尤为重要。<br />
<br />
Z：冒昧的问一句，你从金融危机中学习到了什么？你是否对美国宏观经济的管理失去了信心？一个中国朋友这样对我说：“我们就像课堂上的学生，听美国的教导，但是现在看看，可能这个老师也不知道他在讲什么”<br />
温：在金融危机面前，任何对国家，对全体人类有责任感的人都会从中学习。我学到最重要的一课是，在管理国家事务时，关注经济结构非常重要。中国在发展过程中取得了巨大的进步，赢得了世界的赞誉。然而，包括我在内的一些人，很早就表明我们的经济发展仍然缺少平衡性、协调性和可持续性。金融危机加强了我的这一观点。一方面，我们必须要防止金融危机的影响，另一方面，我们仍然要强调我们自己的问题。这两个方面的工作要同时做好是很难的。<br />
中国有巨大的国内市场，内需有相当大的潜力。中国仍然处于加速城市化和工业化的阶段。刺激国内需求可以巩固并进一步促进经济发展。这就要求我们必须抓住机会，提高发展速度，稳定中国经济。在这一基础上，我们必须着眼长远，清楚的意识到在经济结构中的各种挑战。至于美国的经济，我始终认为它有牢固的基础，不仅仅是在物质上，更重要的，在科技和管理人才这两方面都有着强大的力量。200年来美国在经济发展上积累了丰富的经验，尽管其中有曲折往复，但是我相信，美国会克服各种困难，度过这一次金融危机，要对美国经济的前景充满信心。<br />
<br />
Z：你曾经撰文纪念过你曾经的领导，胡耀邦。这篇文章给我留下很深的印象，在其中你高度赞扬了他。回顾过去，你认为胡耀邦是一个杰出的中国领导人吗？<br />
温：是的。我想我给他的历史做出了一个公允的评价。他为中国的改革开放做出了贡献。第一，他大力推动了有关真理标准的大讨论，通过这一讨论，他为中国解放思想工作做出了重要的贡献。第二，面对众多阻力，他毫不畏惧，为在文革中遭受迫害的广大党政干部平反冤假错案。第三，他采取行动，切实推进了中国的改革开放事业。<br />
<br />
Z：在你的演讲中，你提到从经济上看，中国依然不是一个强大的创新型国家。有着如此多的言论自由限制以及互联网管制怎么可以成为一个强大的创新型国家呢？难道不应该取消这些限制吗？<br />
温：我认为言论自由对每一个国家，不论是发展中国家还是发达国家，都是必不可少的。中国宪法保障言论自由。我认为在这一点上你并不了解中国。在中国，有4亿左右的互联网用户和8亿左右的移动电话用户。他们可以接入网络，表达他们的观点，包括批评意见。我在网上经常能看到对政府工作的尖锐批评和建设性意见。我经常说，不但要让人们享有言论自由，更重要的是，要创造条件让他们能够对政府的工作提出批评。只有人民能够监督和批评，政府才会做得更好，公务员才是真正的为人民服务。当然，监督和批评必须在宪法和法律允许的范围内。这样国家才会有秩序，秩序对于一个有13亿人口的国家来说是更为重要的。<br />
<br />
Z：温总理，说实话，我去中国的时候，发现很多我要上的网站都被屏蔽掉了，非常难以获得资讯，似乎任何挑战共产党政治地位的言论都是不允许的。比如胡耀邦，直到您写的文章出版，在《日人民报》上从未被提起。给我的感觉就是这些限制――对互联网监控的这些设备，让你的人民很难真正有创造性，去做那些你想让他们做的事。<br />
温：我相信，我和所有的中国人民都有这样的信念，那就是中国会有持续的进步，人民对民主和自由的向往和需求是无法阻挡的。我希望你能慢慢看到中国正在持续的进步。<br />
<br />
Z：在过去几周，你做了一些非常有意思的演讲，在深圳的讲话真的把我震住了，你说“不仅要经济体制改革，还要推进政治体制改革”。这也是在我们上一次专访中你的观点。但是很多中国人说过去六七年间，经济体制倒是在不断改革，但是政治体制改革并没有多少。那些听你的演讲的人，他们说“我喜欢温总理的讲话，但是我们并没有看到政治体制改革的行动啊”，你对此有什么看法？<br />
温：其实，政治体制改革是在多年以前由邓小平提出并推动的，我想任何一个对国家有责任感的人都应该对这一点深入思考并投诸行动。上次见面之后我对这一问题展开了更深入的思考，我的想法是，一个政党在取得政权之后，应该要和在取得政权的过程中不一样才对。最大的区别就是，政党的活动应该要符合宪法和法律的规定。政党的政策和主张可以通过合法途径上升到宪法和法律<br />
的高度。任何党派、组织和个人都不得有超过宪法和法律的特权，必须以宪法为根本的活动准则。我认为这是现代政治体系中的重要特征。我将我的政治理念归纳为四句话：让人有尊严的幸福生活，让人感到安全可靠，让社会充满公正，让人对未来充满信心。<br />
尽管社会上存在各种各样的议论，尽管存在各种各样的阻挠，我仍然要坚定不移在我能力范围内，贯彻我的理念，加快政治改革的步伐。<br />
我想用两个词来表达我的决心“风雨无阻，至死方休”。<br />
<br />
Z：货币问题最近又成为了热点，让我这样提这个问题：在得到中国的保证后，人民币在过去两年只升值了1.8%。中国对人民币再多升值一些难道不感兴趣吗？<br />
最近，中国通过压榨普通工人，对出口进行了隐形的补贴。这样就会有通胀的风险，造成了你刚刚提到的缺乏平衡性。因此，人民币汇率要再提高难道不是一件好事情吗？<br />
温：我想你的观点仍然是美国的观点，或者说，美国国会中一小撮人的观点。中美两国经济联系紧密，双边贸易已经超过300亿美元，美国在中国的投资已经超过60亿美元，中国已经购买了超过900亿美元的美国国债。<br />
没有人会相信中国的领导人没有紧跟美国经济的发展。然而，一些美国人，特别是美国国会里面的一些人，对中国缺乏全面的了解。他们将中美关系中的问题政治化，特别是两国贸易不平衡这一问题。我认为这不是一件好事。<br />
我感谢你给我这个专访的机会，因为你让我能够更进一步解释实际的情况。有关汇率和中国贸易顺差，有三个要点往往是大家都不了解的：1.中国并没有主动追求顺差。我们外贸的目标是在各国交易中维持平衡和可持续贸易，我们希望在我们的支付科目中能够达到基本的平衡。这是我们一直在强调和行动的。在2008年，中国的贸易顺差占GDP的比重是9.9%，2009年这一比例下降到5.8%。2010 年的上半年的数字是2.2%。2.一个国家贸易顺差的增长和它的货币政策并没有直接的联系。从1994年开始，人民币启动了改革汇率的进程，从那时算起，人民币兑美元汇率上升了55个百分点，同时，主要经济体和中国周边国家的货币大幅度的贬值。中国对外贸易的急速提高也是在这一时段。实际上，在美国历史上也有这么一段时间，19世纪70年代到20世纪70年代，美国是一个贸易顺差的国家。这一现象，是国家发展的必经阶段。第三点是更为重要的一点，你恐怕也知道，中美两国贸易不平衡的问题主要是结构性的。中国在加工贸易上有顺差但是在一般贸易上存在逆差。交易物品上中国有顺差，但是在交易服务，在交易服务上有逆差。中国在和美国以及欧盟的国际贸易中有顺差，但是在和日本，韩国以及东盟国家的交易中存在逆差。很多出口到美国的商品在美国已经是不生产我想也不可能再重新生产了的――那些附加值比较低的产品。即便美国人不从中国买这些产品，你们也会从印度、斯利兰卡或者孟买购买。这并不能解决两国之间贸易不平衡的问题。<br />
我回忆起你曾经举过有关ipod的例子。在美国一个ipod售价是299美元，但是加工ipod的中国工厂只能从中获得4美元的加工费。<br />
还有一点可能你和国会中诸君都不太了解，在华的5万美国公司中，2万2千家是出口公司，制裁中国的出口公司无异于制裁美国的公司。<br />
<br />
