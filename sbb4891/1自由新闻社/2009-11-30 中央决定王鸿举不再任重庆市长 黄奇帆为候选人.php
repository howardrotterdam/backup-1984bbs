由于资讯不足，俺前一段对渝打黑有些看走眼。随着资讯的不断丰富，俺终于看清了实质。<br />
-------------------------<br />
综合对黎强长达6天的审庭和传媒披露的各种消息，重庆“打黑”的起因现在基本清晰了：<br />
1、大公子的老婆到南山酒店吃饭，重庆的民国遗民们没把勃夫人放在眼里，结下梁子了；<br />
2、出租车主们怕乘客减少不满上边涨价，黎还与公子顶嘴、闹罢运，大公子生气了；<br />
3、相关部门没有及时为大少爷出气，大少爷震怒了，发飙了；<br />
4、结局就是大家看到的样子了。 <br />
==============================<br />
你好，我记得在另外一个帖子里回过你这段话，当时我说的也不是很详细。<br />
&nbsp;&nbsp;1、勃的老婆带朋友（外地来的官太们）去南山玩，晚上在那酒店吃饭（具体名字不好说，那酒店副董是我朋友），包场。南岸区的公安局 长（后来听朋友说轮到他那天做东了）也带朋友去。一方要包场，一方自以为是地头蛇，更因为他根本没搞懂对方来历，结果就闹起来了，结果是那局长和其他人当场被控制了。时间是星期六晚上大约7点40的样子，我当时也和朋友们在南山玩，也准备去吃饭，就看了热闹。过了几天，好象是星期三，报纸上就登了那局长因贪污受贿被双规了。 <br />
&nbsp;&nbsp;2、出租车罢运的原因很多。最大的矛盾是加气难，实际上重庆出租车并不是冬天加气难，去年从大约4、5月份开始就加气相当难，至11月1日根本就停止加气了，其中原因自不用说。其他主要原因有：交警、运政随意抓出租车罚款；城里公厕少，上厕所难；份子钱（板板钱）太高等。后面新闻报道勃参加协调会，说司机们要求涨价，是错的，实际上没人要求涨价，而是要求降低板板钱，也就是要政 府降低运营牌照费。<br />
