<br />
　　出身贫寒的中国内地房地产大亨潘石屹坦言,自小爱财求财,人生要往富处活;而生于富裕家庭的台湾“名嘴”陈文茜,则称人生没有贫穷过,追求的是如何不让自己富有。据香港中通社报道,对话一开始,房产大鳄潘石屹就开始讲述自己的贫穷经历。他回忆说,自己童年时家里很穷,妈妈病了家里无钱医治,家里养不活妹妹,就只能将她送人。“当我后来自己也成为父亲了,才能感受到那份骨肉分离是怎样一种伤痛。”潘石屹称,因为贫穷过,他曾经非常渴望拥有财富,但是当他真的拥有财富的时候,才发现,人必须有两扇翅膀才能飞翔,一扇是物质的,另一扇是精神的。<br />
　　就在潘石屹回忆痛苦童年之后,陈文茜则接过话筒称自己的人生经历和潘石屹完全不同,“我从来就没有贫穷过,因为我父母是台湾的有钱人家,我生下来就没有过过苦日子。”但是,富贵一辈子的陈文茜却总结说,财富带给人的不是幸福而是毁灭。从小目睹了太多亲朋好友为了家产反目成仇的故事,陈文茜称自己很早就明白,钱是会害死人的。因此,她现在给自己定下了生活原则:收入要比支出高,但不能高太多;坚决不投资房产;不买奢侈品。<br />
<br />
　　“老潘,有钱人要好好花钱”<br />
<br />
　　潘石屹与陈文茜不仅对金钱的追求态度相异,用钱观也不同。潘石屹要求生活从简,“坐车坐低排量的车,坐飞机坐经济舱”,陈文茜赞潘石屹节俭精神虽可嘉,却不值得提倡,理由是富豪从简“不利于促进内需”。不过陈文茜表示,富豪可以买自己能消费得起的东西,享受自己该享受的生活,但是不应把钱拿出来炫耀,不应把钱用在买攀比的名车、珠宝等奢侈品上。<br />
<br />
　　陈文茜并不买账,她一针见血地反驳说:“该花的钱就应该花。像我,从去欧洲留学开始,就一直坐商务舱,因为我身体不好。而且,我今天戴的表、穿的鞋子都不错。我们不能像潘石屹那样,否则就是经济倒退的罪人。”陈文茜表示,有钱人花费是必须的,但不能炫富。陈文茜更透露,她去台湾首富郭台铭家里发现,郭台铭的书桌简直破烂不堪,甚至卧室一度让她以为是佣人房。陈文茜半带调侃半带认真地总结:有钱人不好好花钱就是坏榜样。陈文茜说,“中国推崇的先富政策并没有错,但富豪们不应该只是炫耀财富,而应珍惜自己得到的历史机遇,要以一种更负责任的态度去思考,如何带动更多的中国人致富。”<br />
<br />
　　精神财富是坚硬的壁垒<br />
<br />
　　潘石屹把人的发展比作鸟,要有两个翅膀,物质财富与精神财富并重。“如果单纯为了追求物质财富,这样只有一支翅膀,是飞不起来的。”他剖析说, 一个公司,如果能诚实经商,可能会发展速度慢,但总体是安全的;无论是社会、公司还是家人,诚信都不可或缺,爱、责任感、团结等精神财富将是一个坚硬的壁垒。<br />
<br />
　　陈文茜则说,中国现在处在一种摸着石头再过河的状态,不仅是中国政府要创新发展模式,中国的富豪们要思考如何带动更多人致富,中国的知识分子也有责任引领大家朝正确的方向行走。<br />
<br />
　　陈文茜称韩寒说话像放屁<br />
<br />
　　陈文茜身边坐着一位不大言语、身材修长消瘦的年轻小帅哥。陈文茜亲热称小伙子“戡戡”,原来帅哥是李敖17岁的儿子李戡。陈文茜介绍他时,他立即站起身拿出一本新书在身前,他是为了宣传自己的新书《李戡戡乱记》而来。陈文茜告诉记者,李戡的这本书是将台湾教科书各个版本中的诸多弊病收集、整理而成的一本戡乱记,她特地为该书写了长达3000字的序言,而李敖则写了2万字的导读。陈文茜说,李敖冒着30几度的高温,步行到自己家里邀请她写序,“我没有理由不帮助这位有独立思想的青年,他太像李敖了。”李戡确实像父亲一样狂傲,他称自己这部作品是“划时代的著作”,原因是他继承了父亲的光环、赶在自己18岁未成年之前出版并且引起了很高的社会关注度。<br />
<br />
　　同样是青年,有记者问陈文茜对韩寒怎么看,陈文茜说,“用李敖的话说,韩寒不值得评价。她说:“作为上海市民,赛车先生的韩寒看起来蛮帅,其实没有多少文化底蕴。对于上海世博的无知,显得浅薄和没文化,说话就像放屁一样轻松。”<br />
<br />
