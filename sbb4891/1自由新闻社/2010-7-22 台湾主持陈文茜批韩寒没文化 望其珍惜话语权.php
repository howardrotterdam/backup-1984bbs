																											<div id="postmessage_753401" class="t_msgfont">2010-07-22 07:19:26　来源: 华商报(西安)　跟贴 124 条 手机看新闻 <br />
昨日上午，香港书展正式开幕，台湾政治活动家陈文茜成为第一个出现在媒体面前的书展嘉宾。随她一起在媒体面前亮相的还有一位17岁的少年――李敖之子李戡。有记者问陈文茜对韩寒怎么看，陈文茜说，“用李敖的话说，韩寒不值得评价。”<br />
<br />
华商网-华商报7月22日报道&nbsp;&nbsp;昨日上午，香港书展正式开幕，陈文茜成为第一个出现在媒体面前的书展嘉宾。随她一起在媒体面前亮相的还有一位17岁的少年――李敖之子李戡。<br />
<br />
1998年，陈文茜被《亚洲周刊》英文版评选为全亚洲25位“创造趋势人物”之一，李敖称其为他所见过的台湾最聪明的女人。之后陈文茜便退出政坛从事主持和写作并依旧延续她自信敢言、鲜明犀利的风格。现任台湾电视电台节目主持人。 昨日，陈文茜与即将到北京大学读书的李戡一起接受媒体访问，而两个人身上共同的特点就是“批判”，两代人分别批判什么？李敖之子是否继承李敖衣钵？<br />
<br />
批判中国富翁应该对祖国负责<br />
<br />
陈文茜博学广闻，思想独立，她称是因为受到李敖的影响，李敖可以称之为她独立人格之庙的庙公，她昨日的矛头指向了中国的富豪。“现在国家富强，可以说，中国人走到这一步很不容易。我非常担忧中国未来的发展，现在中国人对奢侈品的追求，已经仅次于美国，成为全球第二大奢侈品的消费地区，这是很可耻的事情，中国没有把原来的财富去培养未来的中国的品牌，只是停留在代工阶段。而现在中国富豪呢，想尽办法把他的孩子变成不要中国的外国人，大着肚子去美国生孩子，就想拿到一张美国身份。”<br />
<br />
她说她并非是攻击富豪们获得财富的过程，而是取得财富之后的态度：“中国崛起的时候创造了这些富豪，现在，他们应该对中国的未来负责，而不是把财产都去购买国外奢侈品，或者干脆把财产转移到国外，中国未来如果没有更大的投资、更好的人才，前景非常让人忧虑。”<br />
<br />
批判韩寒应珍惜网民的崇拜<br />
<br />
虽然韩寒今天即将在香港亮相，但陈文茜并未有所顾忌。对韩寒，她先引用了李敖的话：“韩寒不需要谈，没底蕴，长得帅，过几年就跟马英九一样，再美的男子不过如此，他的知识基础不够，他红了也好，不红也是理所当然。”<br />
<br />
陈文茜自己对韩寒的评价却是：“韩寒还是可爱的孩子，又是赛车选手，充满了传奇，但是我不喜欢他有些话，有些话被西方媒体拿来讨论世博会，我自己四次深入采访了上海世博，上海世博有很多韩寒赛车选手先生不了解的地方。”<br />
<br />
她也对韩寒提出了自己的劝告：“他敢讲，他是很多人眼中的英雄，但是他要珍惜他的话语权，他不再是一个凭快感冲刺的人，他的影响力这么大，被时代杂志评为风云第二号人物，应该珍惜中国网民对他的崇拜。”<br />
<br />
批判台湾教科书太狭隘<br />
<br />
李敖之子李戡虽然还没有过18岁生日，但是却已经露出李敖的锋芒，他拿着自己的新书《戡乱记》出现在媒体面前。<br />
<br />
他称李敖对他影响很大，李戡称自己这本书的内容就是揭露台湾教科书内容的残破不堪。为了写这本书，他详细地比对了各种教科书的内容，寻找它们在知识上的缺陷和盲点，例如，在中国五四之后的文化部分，台湾教科书忽视闻一多和毛泽东等重要的诗人。“我书里没有谈到教育制度问题，我不觉得制度会耽误我什么，说白了，状元也是在统一制度下诞生出来的，台湾的十几万考生是公平竞争，大家都在同一个制度下，成功的人还会成功，可是，教科书的内容会影响人的一辈子，我认为这是最严重的。”李戡对台湾的教科书极度不满，所以，他在考上台湾大学后放弃，选择了北京大学，认为自己要“到更深厚的土壤上去寻找祖国文化”。<br />
<br />
陈文茜称赞李戡像李敖一样敢讲，不畏惧，而李敖也尊重儿子去读北大的选择，并且在陈文茜的节目上给李戡做广告：“北大的女生，李戡来了！”<br />
<br />
陈文茜简介<br />
<br />
陈文茜，台湾地区知名人士，陈文茜1958年生于宜兰，在台中的外婆家长大。何家在台中富甲一方，外公何集璧曾参加过共产党，也参加过“二・二八”事件。1980年台湾大学法律系毕业，自2005年起主持凤凰卫视“解码陈文茜”节目，与赵少康，李敖并称台湾三大名嘴。</div>

							
							
							
							
