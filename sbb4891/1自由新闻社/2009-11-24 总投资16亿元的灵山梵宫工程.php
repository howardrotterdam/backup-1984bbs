													<h2>总投资16亿元的灵山梵宫工程</h2>
						
						
																											<div id="postmessage_340748" class="t_msgfont">转自： 柘将军的空间<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;<a href="http://hi.baidu.com/%D5%D4%E8%CF/blog/item/4098961e65c8ad69f724e4c8.html" target="_blank">http://hi.baidu.com/%D5%D4%E8%CF ... c8ad69f724e4c8.html</a><br />
<br />
<br />
灵山梵宫<br />
<br />
――世界级佛教艺术殿堂&nbsp;&nbsp;工程总投资：16亿元<br />
<br />
工程期限：2006年――2008年<br />
<br />
<img src="http://i892.photobucket.com/albums/ac121/wodetuchuang/3732369762_e7d137e339_o.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 2009年3月28日，第二届世界佛教论坛开幕式在江苏无锡举行，来自世界近50个国家的高僧、学者汇聚于此。这场佛教盛典也让世界见识了灵山梵宫――一座融合佛法与艺术于一体的世界级佛教艺术殿堂。<br />
<br />
　&nbsp; &nbsp;灵山梵宫位于无锡灵山大佛景区内，是灵山胜境继灵山大佛、九龙灌浴之后的第三期工程，构成灵山胜境的三大奇观。灵山胜境景区于1994年奠基，分三期工程建设，总投资26亿元。目前已建成国家5A级旅游景区，年接待中外游客数百万人次，成为驰名中外的佛教景点。<br />
<br />
　&nbsp; &nbsp;梵宫位于灵山大佛东侧，背靠灵山，南向太湖，以南北为轴线，东西呈对称分布。面宽153米，进深182米，地下一层，地上三层，总建筑面积72000平方米。内部各建筑空间独立且互相贯通，从南往北，依次分布着门厅、廊厅、塔厅和圣坛。东西两侧则为千人宴会厅和三传会议厅。整体建筑采用中西合璧的建筑风格，以石材等坚固耐久的材料为主材，集中了国内顶尖工艺美术大师和建筑团队，汇集了东阳木雕、敦煌壁画、扬州漆器、景泰蓝、寿山石雕、景德镇青花粉彩缸等众多艺术珍品；其用料之奢华、设计之精巧、布局之大气，堪称巧夺天工，被誉为佛教艺术的“卢浮宫”。<br />
<br />
　&nbsp; &nbsp;2008年11月23日，集文化、艺术、旅游、会议等功能于一身的灵山梵宫正式开启大门。<br />
<br />
<img src="http://i892.photobucket.com/albums/ac121/wodetuchuang/3732369768_83ef9fc975_o.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
 突破传统寺庙建筑形制<br />
<br />
　&nbsp; &nbsp;灵山梵宫的建造源于世界佛教论坛的召开。2005年，江苏无锡被选为第二届世界佛教论坛的举办城市，坐落于太湖之滨的灵山胜境被认为具备举办论坛的条件。以四家国有企业为主要股东的灵山实业有限责任公司，承担了建设论坛会场的任务。为此，灵山实业有限责任公司的董事长、江苏省佛教协会副会长吴国平和他的同事们2006年去杭州观摩了第一届世界佛教论坛。对会场所应具备的主要功能有了比较清晰的了解，但对于整个建筑要做成什么形式，仍然没有很具体的概念。<br />
<br />
　　转机出现在吴国平的一次出国考察之后。他参观了欧洲的许多教堂，如圣保罗大教堂、米兰大教堂、巴黎圣母院等，发现西方的教堂往往高达几十米，高大的空间能凸显宗教的神圣感，让人产生敬畏之心，相比之下，中国传统的寺庙建筑，高度则因受到木结构的限制而显得相对狭小。由此，灵山实业公司对建筑设计上的总体要求是，“希望借鉴欧洲建筑对空间感的塑造，打造一个融宗教、艺术于一身的世界级圣殿，让每个进去的人都有一种崇敬感。”<br />
<br />
　&nbsp; &nbsp;建设之初，灵山实业请来了国内最顶尖的几家设计单位做了几十种方案，其中天津大学做了5个，华东建筑设计院做了17个。最后，经过讨论和筛选，留下5个比较优秀的方案。建设方将“五选一”的最终选择权交给了灵山的游客。2006年“五一”黄金周期间，五种方案分别被做成效果图摆放在灵山景区内的显眼位置。调查进行了三天，收回700多份问卷，近70%的游客选择了其中唯一一个较为古典的方案，这就是今天的以五座华塔为基本造型的梵宫。<br />
<br />
　　2006年9月9日，灵山梵宫正式破土动工，由中国建筑第八工程局承建，施工人员近万人，于2008年8月基本建成。<br />
<br />
比在故宫施工还要难<br />
<br />
　&nbsp; &nbsp;梵宫的内部，举目可见东阳木雕、敦煌壁画、扬州漆器、景泰蓝、琉璃、温州瓯塑等等艺术珍品。能将如此众多的中国非物质文化遗产纳入一座建筑的，目前似乎只有梵宫，也因此，有人称它为“东方的卢浮宫”。由于梵宫颠覆了中式宗教建筑的传统理念，舍弃了中国传统佛教建筑惯用的木头、琉璃瓦等材料，而用钢筋混凝土建造，因而为设计施工带来了很大难度。<br />
<br />
　&nbsp; &nbsp;设计团队为了做好设计，深入了解佛教文化，查阅资料、请教专家、四处考察---远至五台山、云冈石窟、敦煌乃至印度、尼泊尔。设计图纸到了2007年底还有过一次重大修改。因为业主认为石材虽然能塑造空间感，但过于冷硬，而木制品则更具亲和力。后来廊厅8米以上的部位融入了大量的木雕构件，而塔厅从上到下则全用楠木、樟木等装饰。大量木雕的运用，显得中国味十足，也打消了一些人认为梵宫建筑会过于西方化的顾虑。<br />
<br />
　&nbsp; &nbsp;这些木雕均出自浙江东阳一班木雕艺人之手。浙江东阳是中国木雕之乡，北京故宫及苏、杭、皖等地都有精美的东阳木雕留世。据统计，整座灵山梵宫的木雕装饰面积达1.5万平方米，用掉2400多立方米的原木。比如一座塔厅高就有65米，跨度为46.6米，面积为2000多平方米，这样的空间规模在东阳木雕装修工程中，从无先例，这比在故宫施工还要难！为此东阳几乎倾全县之力，42个厂家，1560多名工匠，从2007年底接手，连续制作1年零4个月才最终顺利完工。<br />
<br />
镇宫之宝<br />
<br />
　&nbsp; &nbsp;梵宫的镇宫之宝，是位于塔厅尽头的一幅巨型琉璃壁画《华藏世界》。围绕佛教中的地、水、火、风、空为创意主旨。它由160块1米×0.5米的琉璃构件组成，总宽8米，高10米，外部镶嵌一个青铜边框。琉璃是佛教“七宝”圣物之一，因其“火里来，水里去”的工艺特点，被认为是千年修行的境界化身。除琉璃外，壁画中还镶嵌黄金、翡翠、珊瑚、玛瑙等佛教“七宝”圣物12.8万颗，涉及镶嵌、錾活、花丝等多种中国传统工艺。中央的毗卢舍那佛，内胎用500公斤白银制作，外部则用了15公斤黄金。这幅巨型琉璃壁画的表现内容和艺术价值，不但在中国琉璃工艺史上前所未有，即使在世界琉璃的历史中，也极为罕见。<br />
<br />
　　位于梵宫圣坛前端的大厅，由六个直径近10米的穹顶构成，每个穹顶上均绘有神秘典丽的敦煌壁画，给人营造了一种洞窟式的视觉效果。这些壁画的作者是敦煌美术研究所所长侯黎明，以及代表着当前敦煌壁画创作最高水平的数十名画家。从2008年5月开始，画家们在现场绘制这些穹顶壁画。他们每天都要爬上高高的脚手架，就像米开朗基罗绘制西斯廷教堂天顶画一样，仰头工作十几个小时。<br />
<br />
　&nbsp; &nbsp;灵山梵宫建筑群中有一个鲜为人知的“亮点”，那就是梵宫中所运用的灯光技术是“见光不见灯”。“见光不见灯”的奥秘在于建筑顶部的巨型凿井。这个凿井是一层一层不同木构件的组合，通过木雕构件，背后透上灯光，步入梵宫大厅，游客会感受到灯光带来的庄严和神秘，那就是照明设计师的匠心独运――“见光不见灯”。<br />
<br />
建筑布局<br />
<br />
　&nbsp; &nbsp;梵宫采用对称退台式布局，从南往北，依次分布着门厅、廊厅、塔厅和圣坛。东西两侧则为千人宴会厅和三传会议厅。顶部为错落有致的五座华塔，后侧为曼陀罗形态的圣坛。大量运用高大的廊柱、大跨度的梁柱、高耸的穹顶、超大面积的厅堂等，既体现佛教的博大精深与崇高，又将传统文化元素与鲜明时代特征相融合，一座地标式传世建筑得以完美呈现。<br />
<br />
　&nbsp; &nbsp;门厅是梵宫的交通集散枢纽。门厅面宽约110米，进深18米；东侧通道通往餐饮区域，西侧通道通往会议区域。正面三扇仿石窟造型的金色大门是梵宫主入口，通往廊厅。整个门厅主色调呈佛教传统的明黄色，给人以平静祥和之感。汉白玉铺就的地面清新典雅，庄严而古朴，透明的四格天窗与下方的圆坛遥相呼应，柔和而自然。陈列于门厅两侧高达3.8米的东阳木雕精品“和谐双瓶”，为东阳木雕传人陆光正大师的问鼎之作，寓意和谐美好。<br />
<br />
　&nbsp; &nbsp;廊厅是连接塔厅与圣坛的交通厅，整个廊厅为拱顶结构，高18米，长度近80米，全部采用金丝楠木雕刻而成。两侧16根高大雄伟的廊柱间，十二幅名为“世界佛教传法图”的巨幅油画，每幅高达12米，以时间为序列，以佛教的传播、交流和当代佛教发展为主题，生动展示了佛教传播、交流的历史图景以及佛教文化的丰富内涵，构成了一组视觉盛宴的立体文化长廊。<br />
<br />
　&nbsp; &nbsp;廊厅顶部为方形套叠藻井，中央雕刻有立体莲花图案，各方形藻井整齐排列，布满整个拱顶，让人有一种举首高望空旷辽阔的感觉。藻井七种颜色有节奏地变换，映照着卷棚间高悬的4组仕女飞天，这些采用千年技法“生漆脱胎”造像工艺打造的飞天造型，容貌秀丽，体态丰盈、飞袖飘逸，或弹琵琶、或吹箫，或捧香花，栩栩如生似携彩云飞之欲出，让人如临仙境。<br />
<br />
　&nbsp; &nbsp;廊厅尽头是以琉璃黄金等珍贵材料打造的镇馆之宝大型琉璃巨制《华藏世界》，营造出佛教构建的宏大浩渺、光明灿烂的宇宙境界，堪称文化与工艺珠联璧合的精品之作。<br />
<br />
　&nbsp; &nbsp;塔厅位于廊厅中部，是高潮中重墨浓彩的华章；其位置在中心华塔的下部，为穹顶结构，与华塔上下相通，空间高达60米。高大的空间和精美的穹顶装饰令人震撼，仰天望去，幽蓝色的华塔顶盖繁星闪烁，通透而深邃，仿佛置身一望无垠的蓝色苍穹之下，神秘的苍穹似乎近在咫尺，点点的繁星似乎伸手可及，天人合一间，让人浮想联翩，不觉心生清净安宁之意。<br />
<br />
　&nbsp; &nbsp;塔厅无处不雕花，弧形天顶及四周装饰有大量的珍贵楠木为主材雕刻精美的东阳木雕。四周墙面饰有四幅巨型木雕作品，分别以“净、信、孝、和”命名，形象地展示佛教信仰的精神内涵。每幅木雕作品高7.39米、宽4.3米，既通俗易懂，又极具震撼力和感染力，集中展现了佛教与中华文明相互融合、共同发展的史卷，是灵山梵宫中最具艺术文化双重价值，且能集中反映当代佛教艺术成就的精品。<br />
<br />
圣坛穹顶<br />
<br />
　&nbsp; &nbsp;圣坛前厅穹顶是灵山梵宫中一处创意独特、风格鲜明的衔接空间。圣坛前厅由六个直径近10米的穹顶构成，结合圆形穹顶的独特建筑形态，数十名代表当前敦煌壁画创作最高水平的画师历时数月，将一幅幅神秘典丽的敦煌壁画《天象图》在灵山梵宫完美呈现。现场绘制而成的壁画《天象图》，以炽盛光佛、九曜星全图、十二宫等为主要构成元素，以金黄褐色为主调，在稳定中追求动势，凝固而不滞重，流动而不散漫，使每一个穹顶都富有起伏变化的美感。为圣坛前厅营造出庄严华贵、圆融明丽、舒展大气的佛教艺术气息，彰显出东方佛教文化的独特魅力。<br />
<br />
　&nbsp; &nbsp;梵宫中最重要的地方莫过于圣坛，这是一个集会议、演出、参观于一体的圆形场馆，也是世界佛教论坛的主会场。建筑面积达35000平方米，可以举行2000人的大型国际会议。圣坛上方是高度达30多米的巨大穹顶，1500盏金光灿灿的莲花灯瞬间开启，刹那间千余盏由电脑控制的LED光源拱聚穹顶，仿佛星光灿烂的天穹，可以变幻出1100多种灯光变化。三维立体的舞台，可以在1分半钟内，在舞台中央“长出”一棵10米高、12米宽、11万余片树叶的参天菩提大树。同时，在几十台大型投影设备和目前世界最为先进的数字灯光控制系统综合作用下，营造出一种既似如梦如幻、又似身临其境的美妙景象。由林兆华、何训田等组成的国内顶尖团队导演排练的，以弘扬传统文化、启迪心灵智慧为主旨的大型情境演出《觉悟之路》就在此上演。<br />
<br />
　&nbsp; &nbsp;天籁妙音间，一幕幕或神奇壮观或虚幻奇美的场景在时空变换中交替，人随其中，穿越过去、现实和未来的亿万年隧道，经历地狱、人间和天堂的世界转换，心灵在长廊楼阁和花草树木的恬淡中行走，在瞬息变幻的电光火石和*****中历练，在时而飞升的万千彩蝶和神圣的菩提树下感悟生命……完美的声、光、电形式演绎释迦牟尼成为智者、觉者的过程，这场与观者心灵产生无限共鸣的灵魂感应剧将在每个人的心灵深处留下永不磨灭的印象。<br />
<br />
　&nbsp; &nbsp;门厅的东侧二楼是灵山梵宫的千人宴会厅，这里能同时容纳1200人，也是一个兼具展览馆、宴会厅等功能于一体的综合性活动场所。千人宴会厅没有使用一根立柱，正面展现了世界绝门、非物质文化遗产――浙江温州的瓯塑作品，作为中国传统艺术，它将堆漆艺术与现代立体壁画相结合，用油漆、白陶土及各种矿物质颜料为画料，形成了绘画和浮雕两位一体，远看一幅画，近看似雕塑，被誉为“东方的立体油画”。大型的《五彩荷花》、《荷花映日》由中国工艺美术大师周锦云先生创作，成为梵宫的艺术珍品。<br />
<br />
　&nbsp; &nbsp;会议区域位于灵山梵宫建筑西侧，包含底层三个350平方米左右的大型会议厅、贵宾会议厅及休息室;二层6个一百人的小会议室以及3层展览廊和内部办公室;会议区域空间围绕着方形的庭院展开，获得适宜的空间效果。三大语系风格的会议厅各具特色，不仅风格鲜明而且具有多功能性，作为“世界佛教论坛”分会场之一同时，也是展示汉传佛教文化艺术品的展览馆。整个会议区体现三大融入特色：融入世界、融入历史、融入精华。融入世界佛教风格：会议厅按照汉传、南传、藏传三种风格分别成厅;融入历史：会议厅大量启用历史传统文化因素，突出文化风味;融入精华：现代技术与精湛工艺的结合，展示三种语系佛教文化艺术珍品。<br />
<br />
　&nbsp; &nbsp;梵宫不仅仅是一座气势恢宏的建筑，一座艺术荟萃的圣殿，一座丰富璀璨的文化宝库，它把佛教、文化、艺术、科技、建筑完美而和谐地融为一体，带给人们更多的是一种心灵的震撼、一种艺术的享受；它让中国传统民间工艺得到继承发扬，让中国传统佛教建筑得到革新，让中国佛教能更好地传播到世界各地。<br />
<br />
