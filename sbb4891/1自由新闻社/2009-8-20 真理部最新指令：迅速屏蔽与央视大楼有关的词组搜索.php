　　有建筑业内人士就在网上评论道：“库哈斯在CCTV大楼建成以后，意犹未尽，为了证明自己如何高明，如何骗过了13亿中国人，忍不住自己泄露了天机。《Content》书中登出了几幅画面。原来，仅认为是游戏而大大低估了的悬挑，竟然真的蕴有深刻的‘内涵’――主楼是一位双膝跪地的裸女，屁股对着观众，辅楼则作阳具状！”<br />
<br />
　　哇！我们曾经看到的央视总部三维动画，却是一个渐近渐大扑面而来的屁股啊！我们以前怎么也想不通的为什么悬出部悬得越远也越高？为什么两条直楼要呈6度的斜角向外张开，也都找到了答案，原来却是屁股与央视总部的“异质同构”在作怪。<br />
<br />
　　库哈斯终于按奈不住自己的戏谑，用一种赤裸裸的表达方式阐述了之前三缄其口、故作神秘的设计灵感。它这一显摆，却给了当下的建筑界、政界还是公众一种难以言说的尴尬。<br />
<br />
　　据《中国房地产报》（2009.1.7）报道：“新央视大楼的主楼和辅楼据说是设计者库哈斯根据男女性器的形状设计而成。而这一说法也从库哈斯的弟子之一、MAD建筑事务所主持建筑师马岩松处得到证实。”<br />
<br />
　　现在，“大裤衩子”被扒下去了，露出了色情寓意，该叫它啥呢？此刻，我们不得不感慨老百姓的智慧，早已看懂了这“大裤衩子”里面的东西。<br />
<br />
<br />
<br />
　　这种设计曾被广泛反对。中国并非没有人看出设计问题。<br />
<br />
　　1、2003年，有笔名河清的，在得悉央视新大楼设计方案时，曾写了《应当绞死建筑师？――央视新大楼中标方案质疑》一文，刊载于《文艺报》（2003.8.23），人微言轻地呼吁废止该方案。理由是：这是一个违背中国审美精神的“歪门”，像一个双膝瘫地的跪者，同时既极不安全，又极度昂贵浪费：最初预算50个亿，现在传闻已近100亿。而且这样的HighTech建筑，未来的维修费一般都要超过建造费，是一个真正的无底洞。文章发表后，被大量媒体和网站转载，并在全国获得广泛呼应。<br />
<br />
<br />
<br />
　　2、南京东南大学建筑学院教授郑光复先生，也写了《是谁让科学与经济掩面而泣》的文章，指控央视新大楼是一座“邪（斜）门”。尤其郑先生在另一篇《反科学、反经济、反文化的建筑是超前吗？》文章中，很早就敏锐地揭出央视新大楼含有男女生殖器的“不良寓意”：“他（矶崎新）竭力推荐的库哈斯方案，是不是一对生殖器象征？那倒T形楼（这次烧坏的辅楼），是否阳具指天，还带有阴囊？”<br />
<br />
　　这个矶崎新是日本建筑师，本来就是以搞“生殖器建筑”而知名，“情欲主义是渗透在矶崎新隐喻中的一项常见因素。”这位“生殖器建筑”的“大师”，当年是有关方面请来的央视新大楼设计方案的五位评委之一，也正因为他的鼎力推荐而最终定标。<br />
<br />
<br />
<br />
　　3、河清先生曾跟中央美术学院设计学院院长王敏教授谈起过库哈斯在新书《Content》的拼图，他怎么也不能相信，认定是网络上的恶搞，因为现在电脑拼图非常方便。后来他在美国买到了此书，告河清，书中真有这些图像。河清也将这些图片转给了中国美术学院许江院长，他富于勇气，曾在一次全国人大的会议上质询过央视一位副台长。该副台长答曰：库哈斯没有这样的意思……。<br />
<br />
<br />
<br />
　　令人大惑不解的是，设计方案专家评审团却对中标方案下了这样的评语：“这是一个不卑不亢的方案，既有鲜明的个性，又无排他性。其结构方案新颖、可实施，会推动中国高层建筑的结构体系、结构思想的创造。实施这一方案，不仅能树立CCTV的标志性形象，也将翻开中国建筑界新的一页。”<br />
<br />
<br />
<br />
