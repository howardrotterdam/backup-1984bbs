													<h2>本地市民在北京应享受更低房价</h2>
						
						
																											<div id="postmessage_358071" class="t_msgfont">青年周末<br />
<a href="http://news.sina.com.cn/c/sd/p/2009-12-06/101119200689.shtml" target="_blank">http://news.sina.com.cn/c/sd/p/2009-12-06/101119200689.shtml</a><br />
<br />
　　北京房为何不该卖给外地人<br />
<br />
　　北京房价居高不下，且再次飙升，关键原因之一就是，所有的外地人，无论有没有在京固定工作，无论有没有持续稳定地对这个城市建设做出个人贡献，却都以相同的房价在北京买房。<br />
<br />
　　禁止外地人在北京买房，立刻会被指责为“歧视外地人”。此路不通。<br />
<br />
　　问题是，允许外地人与北京市民以相同的房价在北京买房，这公平吗？<br />
<br />
　　承德和瑞士的启发<br />
<br />
　　有朋友近日去承德游玩，回来后愤愤不平。问之何故，朋友答曰，到承德游玩，买了景点门票后得知，在承德有两种票价。卖给外地游客是一种价格，承德当地人却是另一种价格。据朋友了解，同样的景点票，当地人用很低的价格就能买到。朋友说，这不是歧视外来游客吗？为什么要搞两种门票呢？<br />
<br />
　　于是记者将瑞士人的类似做法告诉了朋友。<br />
<br />
　　记者赴瑞士旅游。接待方为每位中国客人办了一张交通通票。瑞士旅游官员告诉中方客人说，瑞士的交通费很贵，办一张通票比上车买票便宜很多。但是，瑞士当地人乘坐公共交通所付的费用比通票还要便宜。<br />
<br />
　　同样乘坐公交，为什么价格不一样呢？这不是歧视外地游客吗？<br />
<br />
　　瑞士官员脸上显出自豪的表情告诉中方客人，我在瑞士工作，向当地政府交纳个人所得税，这是政府对我的贡献给予的回报。外地人不在这儿工作，没向当地政府交纳个税，没有做出贡献，当然不该享受跟我们瑞士人一样的价格。否则的话，是不公平的！瑞士官员用不解的眼神看着中方客人说：“这里面并不存在歧视的问题。”<br />
<br />
　　朋友欣然悟道，不同的票价体现的是一个公平原则，而非歧视。<br />
<br />
　　北京人、外地人买房价格应有别<br />
<br />
　　就买房而言，北京市民和外地人在北京买房，房价应不同。<br />
<br />
　　北京人在北京买房，房价应低于市场价格，低于外地人在北京买房的价格；相反，外地人在北京买房，房价应该大大高于北京市民买房的价格。<br />
<br />
　　换句话说，不是不让外地人在北京买房。外地人要在北京买房，就得掏更多的钱，承受比北京市民更高的房价。<br />
<br />
　　同样的，北京人如果去外地买房，就该承受更高的价格，花更多的钱。外地人在本地买房，也应该得到低于当地市场的房价，低于北京人在当地买房的价格。<br />
<br />
　　也就是说，北京人到外地买房，不应该以同当地人相同的房价在外地买房。外地房价应该区别对待北京人和本地人。<br />
<br />
　　这就如同承德的景点门票和瑞士的公共交通费一样，对待本地人和外地人要有不同的价格。本地人享受到的票价要大大低于外来游客。<br />
<br />
　　“歧视理论”不应牺牲公平原则<br />
<br />
　　我们国家公民的个人所得税是由地方政府征收的。<br />
<br />
　　常年工作在北京的市民个人所得税缴给了北京市，为北京的经济发展做出了贡献。作为北京市民，就有充分的理由享受政府给予的回报。在北京买房时，北京市民就有充分的理由享受更低的房价。同理，外地人的个人所得税交给了当地政府。外地人在当地买房时当然应该享受到更低的房价。<br />
<br />
　　也正因为如此，北京人去外地买房，就没有理由享受当地的优惠房价，而应出高价，花更多的钱；外地人到北京买房理所当然也得花更多的钱。<br />
<br />
　　这样的做法才更符合市场经济下的公平原则。相反，外地人和北京市民以同样的价格在北京买房，才是不公平的。<br />
<br />
　　时下，确实有一种“歧视理论”困扰着公众。<br />
<br />
　　这种理论认为，大家都是人。人人生而平等，任何事情上都应享受同样的待遇，不能歧视。按照这种理论，买房亦如此，北京市民是人，外地人也是人，不能歧视。北京人享受低房价，外地人要出高价，就是歧视外地人。<br />
<br />
　　这种“歧视理论”是用“表面的”公正，替代了实际上的不公平，还是用计划经济时代“一大二公”的思维、方法，解决市场经济下的新问题，结果只能是牺牲公平原则。<br />
<br />
　　现实社会，每个单位都会用各种方式表达对员工工作的感谢和慰劳。逢年过节，分发过节费或物品。平常日子也会以低于市场的价格卖给员工一些商品。这些都是非本单位员工所不能享受的。<br />
<br />
　　试问，有谁会跑到跟自己不相干的单位说：“你们单位分东西该有我一份。你们单位必须以同样的低价把商品卖给我。否则就是歧视我！”再试问，如果一个外单位人员可以享受到与本单位员工相同的待遇，这公平吗？<br />
<br />
　　同理，外地游客可以享受与承德当地人同样的低价景点门票，外国游客可以享受和瑞士当地人一样的公交优惠，承德人、瑞士人都会高喊：“这不公平！”<br />
<br />
　　显然，承德、瑞士的做法都更尊重公平原则，而不在乎所谓“歧视理论”的责难。<br />
<br />
　　进京投资买房牟利更该出高价<br />
<br />
　　有人会说，北京是全国人民的首都，不是哪一个地区的；北京人在北京做贡献，外地人为北京也没少出力。外地人在北京买房，向北京市缴纳房产税，不同样为北京的经济发展做出了贡献吗？理应享受同北京市民一样的房价买房呀。<br />
<br />
　　事实上，很多外地人到北京买房是为了投资，是为了挣钱。外地的“炒房团”进京购房就是典型的案例。他们看准时机，房价较低的时候买进，房价涨时卖出。相比起他们交纳的房产税，挣的钱更多。“炒房团”的集中购买，更使他们可以以较低的价格购房，炒高房价再卖出，从中获利。<br />
<br />
　　显然，这种行为是到北京淘金挣钱。而北京市民却要为高房价买单，这公平吗？<br />
<br />
　　同样，如果大家都到某省买房，那地方的房价必然暴涨。然后大家高价卖出，挣了钱。当地人不得不承受被炒高的房价买房。对那个省的人而言，这公平吗？<br />
<br />
　　相反，外地人在北京买房，炒高了房价，赚取了利润。所以，外地人就应该以更高的价格在北京买房，这才是公平的。<br />
<br />
　　如果北京的房价无利可图，外地的投资者还会来北京买房吗？到北京买房，挣了北京的钱，还要享受跟北京市民同样的房价，也太说不过去了吧？<br />
<br />
　　北京市民应该享受更多的优惠<br />
<br />
　　有个朋友常年居住工作在美国。他说，在美国最烦、最不愿见到的就是收税的官员。他们来了就是向你要钱。但是转脸他就说：“我在美国是很牛的！”问他为什么？朋友言道：“我是纳税人呀。去公园是免费的，开车上高速也是免费的。我交了税，政府就得回报我。”<br />
<br />
　　这就告诉我们另一个道理，如果一个外国人想享受到美国人的种种优惠，他就得在美国工作，向美国缴纳个人所得税。<br />
<br />
　　外地人，他在北京工作并向北京市缴纳了个人所得税，若干年后，他就应该获得与北京市民同样的待遇，可以以相同的低房价在北京买房了。北京人如果在外地工作并向当地政府缴纳了个人所得税后，他也同样有资格享受当地人的低价格在当地买房了。<br />
<br />
　　作为北京市民，应该享受到更多的优惠。比如乘坐公交，应该享受比外地人更低的折扣；游颐和园、天坛应该可以买到比外地人更低的门票。同理，山东人爬泰山，安徽人上黄山，江西人游井冈山，也应该享受到比外地人更低的价格。<br />
<br />
　　外地人游颐和园，不必因为自己买的票比北京人贵而愤愤不平。你来北京工作，向北京市缴纳个人所得税，若干年后你就可以享受同样的待遇。<br />
<br />
　　北京人游黄山，也别因为自己买了比当地人贵的门票就投诉人家。如果你很想享受当地人的票价，那你就到安徽去工作，向当地政府缴纳个人所得税。几年之后你就可以享受到同等待遇了。<br />
<br />
　　困扰的出路<br />
<br />
　　用正确的方法做正确的事。<br />
<br />
　　就外地人在北京买房而言，“歧视理论”困扰理论界、政府部门多年。要跳出这种理论的困扰，前提是要用符合市场规律的思路和办法，解决市场经济条件下出现的新问题。拿计划经济时代的眼光看待时下的新事物，永远都会是格格不入的，既解决不了问题甚至于还会引发新的矛盾。<br />
<br />
　　也许有人会说，北京目前房价高的离谱，政府就是想给北京市民补贴，也拿不出那么多钱呀？的确如此，但这只是问题的一个方面。另一方面，这也会促动政府想方设法抑制房价的过快增长。<br />
<br />
　　一定还会有人说，外地人可以用种种办法，如借用北京人户口买房。这是一个诚信问题。这个问题的解决也要用市场经济的办法解决。办法也一定会有的。文/《青年周末》记者&nbsp;&nbsp;王进&nbsp;&nbsp;供图/CFP</div>

							
							
							
							
