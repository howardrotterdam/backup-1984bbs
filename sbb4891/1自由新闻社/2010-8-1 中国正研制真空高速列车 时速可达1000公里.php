													<h2>中国正研制真空高速列车 时速可达1000公里</h2>
						
						
																											<div id="postmessage_768463" class="t_msgfont">中国正研制真空高速列车 时速可达1000公里<br />
2010年08月01日 14:14法制晚报<br />
据铁道部7月28日透露，世界时速最快、运营里程最长的京沪高铁有望明年提前通车。据悉，我国在建高铁达1万公里。可您知道除了在建的时速350公里的高铁外，我国还在研制跑得更快的、时速可达1000公里甚至2万公里的真空磁悬浮列车吗？<br />
<br />
从北京到广州2300公里的路程，不用坐飞机、乘坐地面交通，同样可以用2个半小时甚至1个小时达到。这就是我国正在积极研发试验的真空管道高速交通。<br />
<br />
记者从牵引动力国家重点实验室课题组了解到，目前他们正在研制时速500-600公里的真空高速列车，这种技术预计10年后实现运营。<br />
<br />
未来两三年内，实验室将推出时速600-1000公里的真空管道高速列车小比例模型。而根据现在的理论研究，这种真空磁悬浮列车时速可达到2万公里。<br />
<br />
技术揭秘<br />
<br />
大气环境下列车时速不宜超过400公里<br />
<br />
西南交大牵引动力国家重点实验室成员、两院院士沈志云接受法晚记者采访时说，目前任何一种地面交通工具，商业运营速度都不宜超过每小时400公里，否则能耗大、噪音超标，难以被市场接受。<br />
<br />
沈志云介绍，真空管道高速交通，就是建造一条与外部空气隔绝的管道，将管内抽为真空后，在其中运行磁悬浮列车，由于没有空气摩擦的阻碍，列车将运行至令人瞠目结舌的高速。而且管道是密封的，可以在海底及气候恶劣地区运行而不受任何影响。<br />
<br />
该课题组成员张耀平做了一个比喻：“管道就像排污管道一样，连接紧密，是一个完全密闭的空间。” <br />
<br />
真空磁悬浮造价比普通铁路更便宜<br />
<br />
张耀平介绍，真空磁悬浮列车只是列车在真空环境下运行，而车厢内并非真空的，乘客乘坐这种高速的列车也不会有眩晕等异样的感觉。<br />
<br />
从造价上来看，目前普通铁路造价每公里1.5亿，其隧道断面大概在6米到8米之间，而真空磁悬浮管道断面只有4米到5米之间，可省去很多钢材费用，所以费用比普通铁路更便宜。<br />
<br />
对于票价，张耀平猜测，如果该技术刚刚投入运行时，其票价应该要比高铁的价格高，但是随着技术不断地成熟更新，其价格也会逐渐降低。<br />
<br />
实验故事<br />
<br />
网上找材料引进真空管道交通概念<br />
<br />
张耀平在接受法晚记者采访时说，美国佛罗里达州机械工程师戴睿・奥斯特于1999 年将真空管道磁悬浮运输概念落实到图纸并在美申请发明专利。<br />
<br />
2001年，与戴睿相识并成为密友的张耀平将这项技术首次引进中国。<br />
<br />
张耀平说：“当时我在中外运工作，感到我国物流运输能力赶不上经济的快速发展，要解决的运输物流最关键问题是交通要快捷安全，我就在网上找快速交通类的资料，于是就发现了戴睿的资料，这样我们通过网络认识了。”<br />
<br />
2002年初，张耀平联系到轨道交通领域两位权威专家――西南交大机车车辆动力学专家沈志云和磁悬浮专家王家素，他们均对此表现出极大的兴趣，觉得能够进行试验研究。<br />
<br />
当年11月，张耀平放弃了在国企每月拿5000多元工资的工作，毅然到西南交大和这些专家一起进行真空管道交通研究，当了一名拿着1500元薪水的博士后。<br />
<br />
2002年下半年，戴睿和妻子来到中国，帮助张耀平和同事在西南交通大学组建了“真空管道运输研究所”。<br />
<br />
张耀平介绍，当时申请国家自然科学基金时，共申请了3次，2006年终于申请下26万元的项目基金。<br />
<br />
2006年底，带着项目资金，包括研究生、教授在内的共有10多位人员开始了研究。<br />
真空磁悬浮列车优点<br />
<br />
1.无需车载电源<br />
<br />
2.安全性高<br />
<br />
3.能静止悬浮，启动耗能很少<br />
<br />
4.运行噪声小<br />
<br />
5.车体轻，适合高频率发车，大大降低路基和轨道成本<br />
<br />
●各国对比<br />
<br />
中美瑞比较我国技术占优<br />
<br />
沈志云介绍，美国和瑞士也在研究真空管道技术。美国搞的是高真空管道交通，大气压甚至只有外界的百万分之一，但成本很高，难度很大，所以很难投入实际营运。<br />
<br />
瑞士则把真空管放在地下隧道中，这同样增加了很大的成本，最终也难以进入实际应用。<br />
<br />
“而我们的方案相对于他们更优，管道内只要降到0.5个大气压，就可以大大减少空气对于机车的阻力，这样，就大大降低了成本。”沈志云介绍。<br />
<br />
沈志云估算，在现有的高速铁路上，每公里只需要增加一两千万就可以建造成这个管道了，而增加的成本将在后来的运行成本中节约出来。这种方案，机车运行速度可以超过600公里/小时。<br />
<br />
张耀平介绍，目前世界上共有美国、瑞士、中国3个国家正在研究真空管道磁悬浮技术，我国已经开始着手试验了，美国和瑞士还在理论阶段。<br />
<br />
