																											<div id="postmessage_615193" class="t_msgfont"><img src="http://imgsrc.baidu.com/baike/pic/item/570f8c588c2080e49c8204c0.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
<br />
据目击者介绍，清晨5点34分，东大桥路往长安街方向正是红灯的时候，路口一辆白色菲亚特轿车已经停下等灯，后面高速冲上来的黑色英菲尼迪轿车一头顶在菲亚特轿车尾部，菲亚特轿车被撞飞并接连转了两圈后才停下，而英菲尼迪轿车接着撞到一辆由西往北转弯的639路公交车车头才停下来。<br />
　　“车上一个小女孩上半身都甩出车窗外了。”目击者说菲亚特轿车旋转时，后座上的一个小女孩半个身子都在窗户外，差点全部甩出来。据639路公交车司乘人员介绍，他们是昨天的首班车，当时车上有约十位乘客，其中一老者被撞时受伤，后被送入医院进行检查。<br />
　　事故发生后，交警在长安街由东往西方向采取临时管制措施。一名目击者表示，消防人员将菲亚特轿车破拆后才将里面的人救出，车上三人均受伤，其中司机受伤严重，随后三人均被送入医院进行检查治疗。目击者还称，留意到英菲尼迪轿车上的四个年轻人均身着高档服装，有人还戴着耳环等饰物，“一看就知道是有钱人，估计是玩了个通宵才出来吧！”有目击者称该车司机在事发后径直离开。<br />
　　被撞轿车司机脑死亡<br />
　　上午8点左右记者赶到朝阳医院时，菲亚特轿车的司机正躺在急诊抢救室的病床上接受治疗。该男子约35岁左右，双眼微合，处于昏迷状态，盖的被子上有两张脑部CT的片子。医院的急诊记录显示，该男子“四肢受强刺激无反应，弥漫脑肿胀”，初诊结果为“急性重度闭合性脑损伤，脑疝”，并标注“随时有生命危险”。<br />
　　上午9点多，受伤男子的三位朋友赶到医院探望，其中两名女子边抹眼泪边打电话。医生介绍病情时称，男子已经处于脑死亡状态，生命体征完全靠药物和医疗器械维持。医生还表示，可以通知家属见最后一面了。据了解，菲亚特司机为浙江人，事故中，他的5岁女儿头部重伤，妻子腿部骨折。目前事故原因仍在进一步调查当中。<br />
　　肇事者逃逸10小时被抓<br />
　　经交管部门初步调查，昨天凌晨5时35分，湖南省津市市30岁的男子陈家，驾驶一辆京N车号的英菲尼迪牌小客车，车内有三名乘客，由北向南行至朝阳区祁家园路口时，将前方等候红绿灯的一辆菲亚特小轿车撞出，陈家所驾车辆又与一辆639路公交车相撞。事故造成菲亚特小轿车司机和其妻子、女儿，以及公交车内一名67岁老人受伤。陈家在发生事故后离开现场。<br />
　　事故发生后，办案民警围绕嫌疑人陈家及其关系人开展了系列调查。民警兵分三路，第一路民警在陈家住所附近蹲守，第二路民警前往陈家工作单位进行蹲守，第三路民警前往首都机场对陈家妻子行踪进行摸排，寻找嫌疑人下落。15时17分，蹲守在陈家住所的民警发现陈家返回住所后，一举将其抓获。 <br />
