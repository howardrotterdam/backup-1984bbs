																											<div id="postmessage_497859" class="t_msgfont">姚明夫妇赴美静待女儿出生 姚宝宝或入美国国籍<br />
2010-03-01 05:35:13　来源: 舜网-济南时报(济南)　 <br />
　　姚明夫妇已经在2月27日下午1点飞赴美国，届时叶莉将在美国待产。如此一来，“姚宝宝”一出生就可能是美国人。 <br />
<br />
姚明将要当爸爸已经是众人皆知的喜讯，根据预测他的女儿有望在五月份左右出生。据最新消息，姚明夫妇已经在2月27日下午1点飞赴美国，届时叶莉将在美国待产。如此一来，“姚宝宝”一出生就可能是美国人。【姚明携叶莉赴美生宝宝 将出席上海世博会开幕式】<br />
“姚之队”总负责人章明基透露，姚明此次回到美国的最大目的就是照顾妻子待产，同时自己也要进行一系列的康复治疗。章明基说：“这次能在上海过春节，姚明和妻子都非常开心，不过由于美国那边还有一些事情要处理，尤其是叶莉需要定期去医院做检查，因此两人着急赶回休斯敦过元宵节了。”另外，举世瞩目的2010世博会将于4月30日在上海开幕，而组委会已经向姚明发出了邀请，希望他能参加开幕式。章明基表示，姚明能否回来还要看叶莉生产的情况。<br />
<br />
根据美国对于新生婴儿国籍问题的规定，也就是所谓的“出生地主义”，如果一个婴儿在美国出生的话，那么这个婴儿就拥有美国国籍。所以如果叶莉在美国生产的话，那么姚明的女儿在出生那一刻起，就成了美国人，一个持有美国国籍的华人。而中国《国籍法》明确规定，不承认中国公民持有双重国籍，定居国外的中国公民如自愿取得外国国籍，即自动丧失中国国籍。所以一旦“姚宝宝”自动获得美国国籍之后，那么她将自动丧失中国国籍。自从姚明进入NBA以来就成为中国体育的一块招牌，“姚宝宝”一出生就成为美国人，多少让人有些遗憾。不过，据上海东方俱乐部新闻发言人透露，姚明目前并没有确定自己的女儿肯定加入美国国籍。 (本文来源：舜网-济南时报 )</div>

							
							
							
							
