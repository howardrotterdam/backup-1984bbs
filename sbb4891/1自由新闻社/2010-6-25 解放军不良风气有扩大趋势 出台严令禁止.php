													<h2>解放军不良风气有扩大趋势 出台严令禁止</h2>
						
						
																											<div id="postmessage_702359" class="t_msgfont">解放军不良风气有扩大趋势 出台严令禁止(图)<br />
沈阳军区27条禁令刹庸俗交往风 违规者通报批评<br />
 来源：解放军报 作者：张爱华 孙兆秋 2010年06月24日<br />
[提要] 沈阳军区深入开展讲政治、讲文明、讲道德、讲俭朴、讲法纪的绿色交往活动，制定出台27条禁令，狠刹庸俗交往之风。他们的做法非常及时、非常必要，符合广大官兵心愿。大力弘扬胡主席倡导的4个方面的良好风气，就需要像沈阳军区这样抓实的、动真的，大力树立部队的新风正气，既抓基层，更抓机关和领导干部率先垂范。只要常抓不懈，就能形成凝聚党心军心的强大力量。 <br />
 <br />
沈阳军区禁止个人用公款相互宴请，严格控制上下级和同志之间请吃吃请。 <br />
<br />
<br />
　　新闻实录<br />
<br />
　　本报讯 过去，频繁接到“红色罚款单”，是不少基层官兵的烦心事；如今，沈阳军区官兵再也不用为这事费神苦恼了。6月10日上午，沈阳军区某旅张干事高兴地对记者说：“军区出台27条禁令后，红色请柬满天飞、吃吃喝喝忙应酬等不良风气再也不见了。”<br />
<br />
　　前不久，沈阳军区领导在基层部队调研发现：官兵交往功利化、实用化、庸俗化等问题日益凸显。不仅交往的范围越来越大、名目越来越多，而且“唯上交往”、“唯利交往”等现象比较突出，使战友间纯洁情谊变了味。据某旅统计，基层干部每年用于人情往来的开支高达2000至6000元之多，士官也在800至4000元之间。<br />
<br />
　　“交往问题连着部队风气，关乎团结和谐，直接影响部队凝聚力战斗力。”沈阳军区领导在一次会议上这样强调。沈阳军区政治部、沈阳军区纪委在广泛征求官兵意见建议的基础上，于5月31日联合下发了《军区部队开展绿色交往若干规定》，从行为管控、喜庆事宜、节日活动、公务事务、监督惩处等6个方面规定了27条禁令，旨在进一步纠治庸俗交往之风，倡导健康文明的交往行为。<br />
<br />
<br />
　　“禁止邀请部队内部人员参加以生日寿辰、孩子出生、子女升学、乔迁新居等家庭事务名义举办的喜庆活动……”记者翻阅《规定》看到，里面既有严格规范节日期间拜访活动的“铁律”，又有控制自由主义的“紧箍咒”，27条禁令条条清楚、项项明了。<br />
<br />
　　为了确保27条禁令落到实处，他们在开通举报电话、在网上设立绿色交往专栏的同时，要求各级领导干部带头抵制庸俗交往，并硬起手腕对20多名违反规定的官兵进行了通报批评。<br />
<br />
　　绿色交往如一缕春风，吹绿了军营，吹净了人心。沈阳军区部队普遍反映，深入开展讲政治、讲文明、讲道德、讲俭朴、讲法纪的绿色交往活动，抓对了方向，抓好了风气，振奋了人心，一些棘手的问题、为难的事情迎刃而解。<br />
<br />
　　如今，健康、纯洁、文明的绿色交往新风尚正在全区部队形成。一些官兵形象地说：“工作是最好的礼物，组织是最硬的后台。只要干得好，谁也不用找；如果干不好，找谁也白找。”<br />
27条禁令摘登<br />
<br />
　　第1条：操办婚丧嫁娶，事前必须向组织报告时间、地点、规模。团级以上单位主官向本级党委报告，并报上一级纪委备案；其他干部通过所在党组织向本单位纪委报告；士官向所在党支部报告。提倡为官兵举办集体婚礼。<br />
<br />
　　第7条：禁止邀请部队内部人员参加以生日寿辰、孩子出生、子女升学、乔迁新居等家庭事务名义举办的喜庆活动和收受礼品礼金。<br />
<br />
　　第8条：禁止邀请部队内部人员参加学习深造、职务升迁、岗位变动、立功受奖等个人荣誉方面的喜庆活动和收受礼品礼金。<br />
<br />
　　第10条：禁止借节日之机送礼收礼，不准领导干部以个人或单位名义随礼送礼。提倡节日团拜、电话和短信问候。<br />
<br />
　　第11条：禁止借本人及亲属生病之机，收受部属或下属单位人员超越正常礼仪范畴的礼品礼金；禁止部属或下属单位人员为达到个人目的借机送礼品礼金；提倡所在单位以组织名义慰问。<br />
<br />
　　第12条：禁止接受来队官兵亲属的宴请和礼品礼金。<br />
<br />
　　第16条：禁止个人用公款相互宴请，严格控制上下级和同志之间请吃吃请。</div>

							
							
							
							
