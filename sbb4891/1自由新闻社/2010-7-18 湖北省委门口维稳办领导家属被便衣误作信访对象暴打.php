													<h2>湖北省委门口维稳办领导家属被便衣误作信访对象暴打</h2>
						
						
																											<div id="postmessage_747456" class="t_msgfont">来源：<a href="http://blog.sina.com.cn/s/blog_500410490100k0fv.html" target="_blank">http://blog.sina.com.cn/s/blog_500410490100k0fv.html</a><br />
<br />
<br />
<br />
<br />
2010年6月23日上午，在湖北省委南大门，发生一起六人围殴一名体弱老妇的恶劣事件，大门几个监控视频清晰地记录了这一暴力场面。经查实，打人者均系武汉市公安便衣，被打者陈玉莲是湖北省政法委综治维稳办领导的妻子，这天他还在河南参加中央政法委召开的会议。2009年5月18日，这位被打家属的丈夫参加全国社会治安综合治理先进集体和先进个人表彰大会，作为先进代表曾受到了胡锦涛总书记的接见并握手留念（见图1）。<br />
&nbsp; &nbsp;一、暴力“六打一”<br />
&nbsp; &nbsp;身心受伤的陈玉莲现已住进了武汉大学中南医院的综合住院部（见图2、3、4），经过几天的治疗，她神志才清醒过来。对于当天的情景，现在回忆起来她仍感到恐惧、屈辱。<br />
&nbsp; &nbsp;6月22日，陈玉莲跟省委政法委领导预约第二天拜见他。6月23日早9：10，她来到省委大门口。哨兵问：“干什么？”她回答“我住桃山村（省委机关宿舍），是政法委的家属，约了政法委领导谈事。”哨兵就说“你给他打电话。”陈玉莲低头用手机拨号时，突然从省委大院里冲出来六个便衣警察，一脚把她的手提包踢出几米远，一个身着黑色圆领衫、红色短裤衩、戴着粗项链的光头男人照着陈玉莲头部就是一拳，照其腿猛踢一脚，把她戴的帽子也打掉了，被打得东倒西歪、眼冒金花的陈玉莲质问 “我是省委大院的家属，你们为什么打我？”光头咆哮：“老子打的就是省委大院家属，就是省长的老婆也打了，怎么样？”接着，六个人围着她，左一脚，右一脚，像踢足球一样在她身上猛踢，数次把她打倒在地。光头一把将她仰面朝天地摔在大理石地面上，她挣扎着爬起来，三个便衣一涌而上，同时用脚猛踢她下身，再次用力把她揣倒地上，上身和头部磕碰在岗亭铁栏杆上，她躺在地上试图用手机求救，便衣们将手机夺下摔毁。她被打倒了又爬起来，爬起来又被打倒，她在六个强悍男人的围殴中，无谓地挣扎着，她被整整群殴16分钟。省委大门口的摄像头清晰地录下这一恐怖的场面。<br />
&nbsp; &nbsp;在现场，华中农大医院教授周旭荣和老伴付万生亲眼看到了这一可怕的一幕。周教授说：一个光头，戴项链的人突然从省委大院冲出来，对着瘦弱的陈玉莲照头就是一拳，还把她往门旁推打。他实在看不下去，就上前劝说：“你们怎么打人，还象个样子吗？”那人却说“别人的事，你别管！”说完把他们往旁边赶。围观者中有一个认识陈玉莲的邻居也上前劝说，“她是省委大院领导的家属，你怎么也打？”&nbsp;&nbsp;“这不是你们的事，不用你们管！”光头恶狠狠地狂哮，似乎要置人于死地。陈玉莲的几个邻居从省委对面的小区跑过来，对这群打人的男人讲，她是省政法委黄厅的爱人，你们不能打了。那六个便衣却说你们叫她家里来人把她弄走。邻居要求他们给政法委打电话，六个便衣警察却扬长而去。又过了近半小时，趴在地上神志不清的陈玉莲，被人用车拉到了湖北省信访中心，关进了铁门里面的公安室，由两名警察看守着。<br />
&nbsp; &nbsp; 一个半小时后，陈玉莲才缓缓清醒过来。她看到面前一双大脚对着她的脸，是一名看守她的公安干警的脚翘在桌面上。她要求打电话给丈夫，远在河南省开会的丈夫，他不敢相信发生在其妻身上的事。他迅速报告给政法委领导，约11：52，陈玉莲才被解救出来，被送往武汉中南医院，她头上，身上，胳膊上、腿上遍布伤痕。诊断结果是，她被打成脑震荡，软组织挫伤几十处，左脚功能障碍，谷物神经紊乱……她躺在病床上，浑身哆嗦，呕吐腹泻不止，发烧几天，身心受到了重创。<br />
&nbsp; &nbsp; 二、 公安机关称：打人是误会<br />
&nbsp; &nbsp; 陈玉莲在省委大门被打。省政法委领导干部家属在省委大门被打……这一消息迅速被传开，人们开始怀疑，但很快得到了证实。6月23日，武汉市公安局、武昌区政法委、武昌区公安分局、水果湖派出所的领导纷纷赶到医院看望陈玉莲，并当面道歉。这一幕似乎有些莫大的讽刺。<br />
&nbsp; &nbsp;&nbsp;&nbsp;6月23日下午5点多钟，武昌区委政法委副书记，武昌区公安分局政委，水果湖派出所所长等一行看望陈玉莲。分局政委的一席话，让在场人非常震惊。他说：“领导知道这事后很重视，你看我第一时间赶了过来。”并说，“误会，纯属误会，没想到打了这个大的领导夫人”。病床旁边几个看病号的人实在听不下去，当场指责：“若是一般群众是不是都可以随便打，你们是不是打习惯了。” 陈玉莲气愤地说“你把我打死算了。”说完，又是一阵呕吐。<br />
&nbsp; &nbsp;《中国共产党纪律处分条例》第143条规定“对他人进行殴打，体罚，非法拘禁，非法搜查，情节严重的给予开除党籍处分”《警察法》第22条，第48条，第50条也规定，人民警察殴打他人或者唆使他人打人的，应当给予行政处分（分为警告、记过、记大过、降级、撤职、开除）；构成犯罪的，依法追究刑事责任；在执行职务中，侵犯公民或者组织的合法权益造成损害的应当给予赔偿。就连《治安管理处罚法》第43条对普通公民结伙殴打伤害他人的，或者多次殴打伤害他人的行为，都要处以10日以上15日以下的拘留，并处500元以上1000元以下罚款。<br />
&nbsp; &nbsp;&nbsp;&nbsp;三、 谁是元凶<br />
