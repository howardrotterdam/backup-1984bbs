 <a href="http://docs.google.com/Doc?docid=0AXVPpUcJldDHZGZyZHJkbjJfMzRkc3gyNnJncw&amp;hl=zh_CN" target="_blank">http://docs.google.com/Doc?docid ... NnJncw&amp;hl=zh_CN</a><br />
<br />
　　创造条件让人民批评政府及监督政府，写入今年两会的政府工作报告。温家宝总理言犹在耳，即发生湖北省长李鸿忠抢夺记者录音笔一事。舆论哗然，为当场强夺之举动心惊，更为其后狡辩之言辞心寒，其谓“我们就担心她不是记者，就把录音笔拿去看看了”。在国会庄严之地，一方大员公然背弃政府承诺，出演暴力丑剧，不惮以轻浮言论搪塞天下，凡有良知之士，莫不出离愤怒。<br />
<br />
 <br />
<br />
　　李鸿忠主政湖北，单就社会民生领域，纷攘轰动事件一起再起。邓玉娇案本是乡镇恶吏作奸犯科，地方官府恃权欺瞒，以杀人罪起诉受害人，监视、殴打、驱赶记者，不惜牺牲法治以遮掩官员颜面。石首事件引燃街头骚乱，李鸿忠畏缩诿责于前期，后以强力收拾残局，坊间余恨迄今未平。再有荆州挟尸要价场面，惨状惊悚国人。湖北公共治理警钟频传，行政乱象可见一斑。<br />
<br />
 <br />
<br />
　　两会既开，民众叩问国是。李鸿忠身为全国人大代表，理应检视执政得失，思量与民为善。察李鸿忠言行，背离政治文明，迹近自我骄横。强夺事件，受害者绝非当事记者一人。媒体权利折戟，大众知情权受挫，国家形象受累，议会殿堂蒙羞。李氏表现，辜负民众信托，有损人大威仪，于国，于党，于民，流弊昭然。<br />
<br />
 <br />
<br />
　　宪法赋予媒体人之权利，乃公民问政之权利，强权悍然践踏，理当据理抗议。坐而论道不如起而行之。老报人周瑞金仗义执言，中宣部新闻局原局长钟沛璋逾八旬，于病榻之上著文呐喊。<br />
<br />
 <br />
<br />
　　在此郑重呼吁，新闻界与知识界同声相应，知耻而后勇，合力声讨李鸿忠事件的恶劣影响。报纸杂志电视电台网络微博，媒体无论类型，记者不分南北，请坚决报道后续事态，迎向此次权利之战。盼望新闻人振臂声援，欢迎公民附议此书。忍无可忍，无须再忍。让天下人知道尚有新闻记者，让天下人知道尚存新闻人格，更要让天下人知道尚有公民权利。李高官暴怒在前，也该让他听到并目睹新闻界之怒，亦是布衣之怒。<br />
<br />
 <br />
<br />
　　吾辈请愿行动均是公开而为，依法而为。吾辈责难李鸿忠，凭借的无外乎是非感与公义心。新闻界诉求目标有二：要求李鸿忠立即向新闻界及公众道歉并辞职；吁求全国人大主席团和秘书处立即启动对其调查及弹劾程序。<br />
<br />
 <br />
<br />
　　因此，提请全国人大常委会查勘李鸿忠不当言行，关注事态发展；专门调查并完整公布李鸿忠强夺记者录音笔的事件真相，责成李鸿忠公开道歉，严肃核查其全国人大代表是否适格，并责令其引咎辞职。<br />
<br />
 <br />
<br />
　　媒体诸同仁，抗议不必苛求胜算，但求无愧于心。兹此抗议，事关业界尊严，更见新闻良知。同业侧身之报馆、广播电视台、网络公司，岂堪一再受辱？社会公义与道德良知，不可反复蒙羞。敬请共襄义旗，守望相助。为新闻界争尊严，亦是为民众争尊严。<br />
