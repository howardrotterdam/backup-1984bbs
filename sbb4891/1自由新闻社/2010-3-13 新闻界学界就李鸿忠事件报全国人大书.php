													<h2>李鸿忠事件继续发酵</h2>
						
						
																											<div id="postmessage_514870" class="t_msgfont">关于两会期间全国人大代表<br />
<br />
夺走记者录音笔事件善后建议书<br />
<br />
 <br />
<br />
十一届全国人大三次会议主席团：<br />
<br />
我们注意到，两会期间全国人大代表抢夺记者录音笔事件仍未得到严肃认真的处理。<br />
<br />
3月7日，就在温家宝总理在政府工作报告中郑重宣示“创造条件让人民批评政府、监督政府”两天后，就在两会会场，竟然发生了全国人大代表李鸿忠同志夺走《人民日报》旗下《京华时报》女记者刘杰录音笔、威胁“我要给你们社长讲”的恶劣事件。<br />
<br />
如果这是一时冲动的失态，事后应该赶紧设法弥补，该解释的解释，该道歉的道歉，让舆论热点尽快冷却，重新回到两会既定议程上来。但李鸿忠代表对这一事件的政治严重性毫无认知，3月11日对媒体辩称“担心她不是记者，就把录音笔拿去看看”，声称此事“不涉及道歉”，“慢慢就过去了”。李鸿忠代表粗暴无礼在先，强词夺理在后，激起了公众特别是网民更为强烈的不满，引起海内外舆论的强烈反弹。<br />
<br />
这样的舆论态势，严重干扰了两会对“一府两院”工作的审议，转移和冲淡了国家最高决策机构谋划2010年“继续应对国际金融危机、保持经济平稳较快发展、加快转变经济发展方式”这一国家重大政策议题。我们几位长期研究经济体制改革、社会转型和共产主义运动史的学者，对此事态发展深感不安。<br />
<br />
新闻媒体是党和政府联系人民群众的桥梁，是实施舆论监督的利器。干部是人民公仆，媒体替人民监督。抢夺记者采访工具这一行为，是对舆论监督的抵触和蔑视；李鸿忠代表在3月11日的表态中说“我觉得不用在网络这个层面再做什么”，又流露出对汹涌的网络舆论所表达的民意臧否麻木不仁和排斥的心理。这些与人大代表的身份是多么不相称！李鸿忠同志接受选区人民的重托来北京参加两会，不该摆出如此官老爷架子，损害人民代表的形象，损害执政党的形象。<br />
<br />
李鸿忠代表同时作为湖北省省长，强力对抗舆论监督，严重破坏了人民代表大会监督政府工作这个国家政权运行的基本原则。在发达国家，议员为了当选和连任，不得不对选民和记者非常谦和。而我们的一些领导干部眼睛向上，对于来自下级和基层的监督，来自社会和媒体的批评，往往视而不见，我行我素，内心深处压根没把人民代表大会的监督当回事。<br />
<br />
网上很多帖文不约而同地提到，李鸿忠代表的言行直接违背了温总理的政府工作宣示，甚至是“给了总理一记耳光”。有些外国媒体评论说，录音笔事件暴露了人民代表大会制度对于官员的“软约束”和“假约束”，导致人民代表大会制度陷入运转阻滞，个别媒体甚至危言耸听说“中国的人大制度陷入危机”。<br />
<br />
人民代表大会制度是中华人民共和国的立国之本，对于巩固国家政权，保障人民当家作主，维护社会稳定和民族团结，具有十分重大的意义。每位中国公民、每位执政党的党员，都要像爱护自己的眼睛一样，爱护人民代表大会制度。诚然，人民代表大会制度还需要在今后的实践中继续完善和改革，更好地体现依法治国、民主决策的现代宪政法则，但决不能容忍个别领导干部以长官意志凌驾于人民代表大会制度之上，把个人公关危机发展为影响政府形象、执政党形象和国家根本政治制度声誉的公共政治舆论危机。<br />
<br />
为此，我们建议，十一届全国人大三次会议主席团讨论这一事件，迅速采取措施，严肃处理这个问题，敦促李鸿忠代表向受辱记者和全国人民公开表示道歉，赶在两会闭幕前平息风波，挽回政治影响。<br />
<br />
中共十七大提出保障人民群众知情权、参与权、表达权和监督权。温总理在今年两会上又提出“创造条件让人民批评政府、监督政府”。各级领导干部都有责任认真领会和坚决贯彻中央决策的战略意图，努力“创造条件”，欢迎和保护舆论监督。遗憾的是，在一些地方和部门，缺乏接受人民监督的诚意，经常表现为粗暴地拒绝和打压新闻媒体的正常报道，敌视和扼杀“网络问政”。李鸿忠代表作为受党教育多年的领导干部，所缺乏的，不仅是对他人特别是对女性的基本尊重，与新闻媒介打交道的公关素养，更是“情为民所系，利为民所谋，权为民所用”的党性修养。建议抓住这个事件，在各级领导干部中开展教育，引以为戒。<br />
<br />
单光鼐研究员和于建嵘研究员年纪相对较轻，没有老一辈党内专家学者对党的优良传统和历史教训的深刻阅历，但近年来致力于群体性事件和社会稳定研究，对于地方领导干部在化解社会矛盾、接受舆论监督方面的经验教训也有一些鲜活的感受。在贵州瓮安，2008年瓮安事件发生后，省委书记石宗源3次向当地群众、机关干部、人大代表、政协委员鞠躬道歉，检讨政府工作的不足，给自己打了个不及格的分数。瓮安农民向单光鼐研究员忆起，石书记鞠躬时头顶那片稀疏的发丝，共产党的干部对老百姓的这种诚挚令人动容。<br />
<br />
中国处在社会转型期，伴随着经济的高速增长和人民生活的普遍改善，还存在收入分配不公，官员腐败，高房价，看病难、看病贵，教育改革积重难返，弱势群体基本生活条件和权益缺乏有力保障……在这样的“矛盾凸显期”，更需要各级领导干部情系大众，视民如伤，牵挂民生疾苦，守土有责，聚拢人心。如果无视民意批评，一意孤行，有违中央教诲，有负民心党心。在此，我们也提请李鸿忠代表本着党性原则，反躬自省，深长思之。<br />
