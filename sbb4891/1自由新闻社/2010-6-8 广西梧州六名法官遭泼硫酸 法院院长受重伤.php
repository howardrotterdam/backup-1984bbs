 <br />
&nbsp; &nbsp; 梧州市有关领导表示，梧州公检法部门要同时介入案件，尽快固定证据，从快从严查处，并及时做好群众特别是犯罪嫌疑人亲属的思想疏导工作，防止不明真相的群众聚众闹事。<br />
