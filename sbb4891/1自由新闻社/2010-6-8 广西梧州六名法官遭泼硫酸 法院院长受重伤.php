													<h2>广西梧州六名法官遭泼硫酸 法院院长受重伤</h2>
						
						
																											<div id="postmessage_666003" class="t_msgfont">中国新闻网6月8日报道 今日(6月8日)广西梧州市龙山大道上发生一起严重暴力抗法事件，六名法官受伤，其中两名法官遭受重伤，目前正在红会医院紧急抢救。<br />
<br />
6月8日上午9点左右，梧州市长洲区法院在市新兴二路龙山里54号，依法对被执行人陈宏生、廖凤娟夫妇执行买卖货款纠纷等案件时，被执行人陈宏生、廖凤娟夫妇从楼上突然向前来执行的法院、公安干警泼下硫酸，致使6名干警不同程度烧伤。其中长洲区法院院长廖克东、执行局局长吴志斌等两名法官伤势严重。<br />
<br />
事件发生后，梧州市公安局特警大队、武警特勤大队等部门迅速赶到现场，抓获了两名犯罪嫌疑人。两名伤员被迅速送至梧州市红会医院救治。<br />
<br />
<br />
据了解，廖克东体表面积50%烧伤，属最严重的三级烧伤。吴志斌体表面积10%烧伤，也属三级烧伤。两人的其中一只眼睛面临失明的危险。据红会医院眼科主任卢兵说：“病人的眼睛现在是急性化学性烧伤。其中一个病人烧伤比较严重，达到三度烧伤，以后恢复比较困难。还有一个是二度烧伤，二度烧伤的话也很重。一个左眼一个是右眼。廖克东是右眼，吴志斌是左眼。”<br />
<br />
上午11点50分，梧州市委副书记刘永明赶到医院看望受伤的伤员，并慰问了伤者家属。并指出：对这样的恶性抗法案件要严厉打击，要尽最大的努力抢救伤员。政法委书记竺坤松表示：梧州公检法部门要同时介入案件，尽快固定证据，从快从严查处，并及时做好群众特别是犯罪嫌疑人亲属的思想疏导工作，防止不明真相的群众聚众闹事<br />
<br />
目前，梧州市红会医院已组成紧急医疗小组，正全力抢救受伤病人。</div>

							
							
							
							
