													<h2>【转帖话题二】“谁叫那些人没事献爱心”</h2>
						
						
																											<div id="postmessage_326219" class="t_msgfont">【转自豆瓣猫组，原帖地址：<a href="http://www.douban.com/group/topic/8660127/" target="_blank">http://www.douban.com/group/topic/8660127/</a>】<br />
