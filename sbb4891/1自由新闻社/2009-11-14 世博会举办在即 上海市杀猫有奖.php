2009-11-13 纪念一只可爱的小猫<br />
<br />
到现在我和妈妈还在难受。一想起来傍晚的那一幕，我眼泪就停不住。实在忍不住，就来这里写写那只可怜的小猫吧。<br />
<br />
大约三年前，我母亲开始喂养流浪猫，小区里也聚来了一些猫友。渐渐地，过来寻食的流浪猫越来越多，小区里开始有人抱怨连连。还有人找物业投诉，说野猫不卫生，对人造成骚扰等等。我母亲她们非常注意卫生，天天看着猫猫们吃完，打扫完，然后再走。<br />
<br />
今晚，母亲去喂猫，她刚走二十分钟。我就听见楼下传来猫的嘶鸣声。我打开窗，看见一只大狗，一群人正围着它....<br />
<br />
我突然感到情况不妙，大声对楼下的人群喊：“别让它咬猫啊！”然后冲出了门。就一阵电梯的功夫，我出去一看...我回来还抱过的那只小白黄猫...倒在地上...咬它的大狗跑没了。我去抱它，它的胸口撕开了十厘米口子....满嘴的血....肚子也咬破了...大便和肠子滚了一地....它眼睛都闭不上...嘴里都是血....<br />
<br />
其实，这只小白花猫是只家猫，不知谁丢了它不要了。那些流浪猫里它最乖，看见人就蹭。就晚上，我回家抱它的时候，它还会翻我的包....才几个小时。它就没了....<br />
<br />
那咬它的大狗，有主人，说她拦不住不咬猫啊，谁叫那些人没事献爱心，招来那么多只野猫....<br />
<br />
我抱着小猫哭的时候。还有个老太太过来说，“你不就是耐嘛。死一只算啥，还有那么多只让你爱啊。”<br />
<br />
我真是哭得没话说.....<br />
<br />
我们去埋猫.....这么大的豪华都市，连埋猫的地方都没有。大傍晚，竟然连把铁锹都借不到。<br />
<br />
整整三个小时....人心比大雪还冷.....都市人，真的很残忍。<br />
<br />
送走它，我母亲说，“对动物都没有爱心的人，比禽兽还不如！”<br />
<br />
――――――――――――――――――――――<br />
<br />
请那些喜欢动物的人，不要等它们大了，就开始讨厌它们，把它们丢掉！<br />
<br />
如果你不能分清什么是喜欢，什么爱的话，请你不要饲养小动物！<br />
<br />
它们不是你的宠物，它们是生命！<br />
