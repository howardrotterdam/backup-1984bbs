问：还不是居民，是你们外围的工作人员说的。植物园马上要闭馆了，上海爱猫的人士都很关心，因为原来的猫阿姨也不能进来喂了，猫咪会大批饿死，我代替上海各大猫咪论坛的猫友们来问问，有没有可能和园长沟通一下，由志愿者进来，负责把猫咪转移走。<br />
答：我们现在已经闭馆在施工，大批人过来可能是不安全的。<br />
问：所以我希望能尽快行动，趁现在你们工地还不是很多的时候。<br />
答：好的，我会请示领导的，请留下你的电话和姓名。<br />
问：这事情搞得挺大的，我去你们网站看了一下，很多留言的，我觉得你们是不是要回应一下。<br />
答：是的我们很重视，正在处理。<br />
大约15分钟后，我接到了电话，这位负责人说，领导答应我们可以转移猫咪去领养什么的，但是要提前通知他们时间。<br />
以上是我们沟通的结果，应该说，不管这个杀猫奖钱行为是否已经发生过，但是他们的回应还是比较快的。而且暂时答应了我们的转移要求。<br />
然后希望大家的方向马上投到转移猫咪上面：<br />
目前，“爱惜生命”承担了大量的筹备和联络工作，配合她的工作，我将明天向植物园申请周三和周末中的一天为志愿者进园转移猫咪，时间大约在三点半到7点。现在周三这天有7-8个朋友已经联系了会过去，小动物协会的也会有人过去。<br />
时间已经很紧了，周三和周末有空有意愿去帮忙转移猫咪的朋友请跟帖报名，同时请告诉我能不能带上笼子和猫包。因为“爱惜生命”没有注册这个论坛，本网站会员的参与工作暂时我来统筹。<br />
“爱惜生命”同时在联系寄养基地和绝育医院，陈小姐在联系抓捕笼子，最坏的情况是直接找安全地点放归，最好的情况是绝育然后亲人的找领养，渺渺今天下午已经到了果果了，5点左右，赵医生帮她做了手术，这个幸运的孩子是我们第一个带出来的猫咪，腋下已经有伤了，还好我们有缘相遇了，我会负责她的看护和领养，如果出院后没有领养人，我会在我家小区地下自行车库边的旧家具废弃区给她搭个猫窝，负责喂养，至少帮她提高存活机会。<br />
