<br />
　　“中国一日”有两层含义：一是作者与作品记录的事件都发生在国内，即“在中国的一日”；二是作者与作品发生地都在境外，但内容与中国有关，即“关于中国的一日”。<br />
<br />
　　二、作品要求<br />
<br />
　　活动主要设文字作品、摄影作品、视频作品等三项。文字作品一般不超过1500字，图片单幅或一组不超过5张，视频作品长度在3分钟左右。作品内容要求高扬主旋律，唱响爱国曲，扬中国魂，抒中国情，写中国人，记中国事，以小见大、见微知著。<br />
