													<h2>温家宝：政改可创造宽松政治环境 共产党政权被宪法约束尚需时间</h2>
						
						
																											<div id="postmessage_841476" class="t_msgfont">温家宝在美谈政治体制改革最主要问题<br />
2010年09月23日 17:46凤凰卫视<br />
<br />
核心提示：美国当地时间9月22日，中国国务院总理温家宝在纽约与美国华文媒体和港澳媒体负责人座谈，并回答了媒体负责人们的提问。在回答美国《侨报》总裁谢一宁关于如何理解政治体制改革与经济体制改革内在关系的问题时，温家宝总理再次强调，如果没有政治体制改革的保障，经济体制改革也不会取得彻底成功。温家宝同时指出政治体制改革最重要的就是保证宪法和法律赋予人民的各项自由和权利，使人能得到自由和全面发展，是民主和自由的主要内涵。<br />
<br />
凤凰卫视9月23日《大陆新闻》节目播出“温家宝：个人的自由和全面发展是民主的主要内涵”，以下为文字实录：<br />
<br />
谢一宁（美国《侨报》总裁）：我是美国《侨报》的。<br />
<br />
温家宝（中国国务院总理）：谢一宁，《侨报》的，《侨报》一共印多少份？<br />
<br />
谢一宁：大概我们是在美国有3个印刷点，在纽约、洛杉矶、旧金山，我们有日报、周报，这三个地方来讲，我们的印刷量大概是在十万份左右，《侨报》从1990年开始，到现在正好20年了，从一开始，一直致力于说能够搭建两座桥，把这个作为我们的宗旨，一座是沟通中美之间的桥梁，一座是沟通海外华人跟他们祖籍国之间的桥梁。所以在这几十年里边，经过二十年的努力，我们在比较准确的报道中国的信息这方面，应该是说做出了一些工作，也得到了读者的一些认同。<br />
<br />
所以今天我请教总理的问题，基本上也围绕着中美两国之间的。延续第一个问题，中国改革开放三十年，在经济上是取得了巨大的成就，刚才讲到了可能超过日本，经济总量成为第二。但与此相伴随的是西方国家，特别是美国对中国崛起的一种戒心，我想请教总理，中国如何应对这样一种局面，特别是在新世纪里边处理好中美两个大国之间如何建立互信；与此相关联的是我们注意到总理最近有几次谈到政治体制改革的必要性和迫切性，想请教总理在经济快速发展的当下，如何理解政治体制改革跟经济体制改革之间的内在关系？<br />
<br />
温家宝：中国的发展西方一些国家确实存在着一定程度上的戒备心理，所谓中国威胁论就是一个代表。我这次在联大的一般性辩论主要是回答这个问题，就是如何正确认识一个真实的中国，其中一个重要的论点，就是中国将坚定不移的走和平发展的道路。中国的发展不会影响任何国家，也不会干涉任何国家，更不会威胁任何国家。所谓国强必霸在中国绝不会出现，何况我们实现现代化的任务还需要很长的时间，这同中国的文化传统以及中国发展的自身需要是分不开的。<br />
<br />
大家知道600多年前的郑和，郑和航海南过马六甲，西到好望角，带去的都是丝绸、茶叶和友谊，没有占过一寸土地，包括像澳大利亚那时还没有开垦的地方。鸦片战争以后，实际上是由于满清的衰败，遭到了列强对中国的分割，我们才沦为一个殖民地和半殖民地国家，所以我一直懂得一个道理，就是一定要平等地对待那些平等对待我们的民族，尊重他人也是尊重自己，因此我们对外奉行的原则最基本的就是互信尊重平等相待，互不干涉内部事务。中国的所作所为，应该让各国放心，中国是一个爱好和平的国家，不仅今天是这样，永远是这样。<br />
<br />
关于政治体制改革，我曾经讲过，经济体制改革如果没有政治体制改革的保障，也不会彻底取得成功，甚至已经取得的成果还会得而复失。我们政治体制改革最主要要解决什么问题呢？我以为最重要的就是要保证宪法和法律赋予人民的各项自由和权利，就是要调动人民群众的积极性和创造精神，就是要有一个宽松的政治环境，使人们能够更好的发挥独立精神和创造思维，就是要使人能得到自由和全面发展，这应该是民主和自由的主要内涵。<br />
<br />
当然，中国要建立一个民主和法治的国家，所谓法治最重要的就是当一个政党执政以后，应该按照宪法和法律办事，党的意志和主张也要通过法定的程序来变为宪法和法律的条文，所以的组织都要符合宪法和法律下行动，这才叫依法治国。我以为做到这一点，可能还需要一定的时间，但是这是现代文明和现代政治所必须的，我们应该朝此而努力，谢谢。</div>

							
							
							
							
