“一小撮”<br />
形容词。原指极少量，现泛指一切不明事实真相的人，无论其数量多少。<br />
<br />
“情绪稳定”<br />
俗语，用来形容受害者或者灾民的情绪。泛指除上街游行和打砸抢烧之外，大哭大闹、虚脱和死亡在内的一切心理和行为表现。<br />
<br />
“驳斥”<br />
动词。指官方用一种非常委婉的方式，对民间传言进行默认或者肯定。<br />
<br />
“严加监管”<br />
动词短语。原指在人民要求下，政府加强对不法企业的监督管理。现词义已改变，指在不法团体要求下，政府加强对人民的监督管理。<br />
<br />
“普世价值”<br />
代词。”民主，自由，法制，人权”的另一种说法。贬义词。<br />
<br />
“代表”<br />
动词。所跟的宾语最低基数一般为13亿。<br />
<br />
“你们算个屁呀！”<br />
名句。其真实含义极为丰富，难以解释。资历较浅者应慎用，否则将以泄露国家机密罪论处。<br />
<br />
“可以放心食用”<br />
短语。代表了中国食品工业目前的最高水准。指食物或饮品买回家，经过高温消毒、洗洁精漂洗、27层过滤、巴氏灭菌法、负离子氧化、高温碳化、氖光线照射等简单工序后，只要摄入量不超过规定剂量，不足以对食用者构成必杀或者秒杀。<br />
<br />
“又好又快”<br />
联合词组，形容我朝经济发展的专用词。</div>

							
							
							
							
