<br />
　随着互联网地图网站的普及，如今，越来越多的人可以从网络上查找交通路线、观看高清晰度的卫星影像、甚至标注自己喜欢的景点、与全球网友分享地标信息。但是，在带给大众方便之余，一些网友在互联网地图上的标注导致了一些国家机密信息被泄露。从去年三月至今年三月，国家测绘局等多家部门对全国地理信息市场进行专项整治，&quot;网络泄密&quot;成为整治的重点。<br />
<br />
　　&quot;月光论坛&quot;泄露涉密地理信息被查处<br />
<br />
　　2010年4月，深圳市规划土地监察支队根据群众举报，发现了一个名为&quot;月光论坛&quot;的网站，存在大量地理信息涉密的行为。网站把大量国家军事上的信息，比如机场、舰艇还有驻港部队的地标等都在地图上展示出来。<br />
<br />
　　月光论坛是一个军事爱好者经常光顾的网络社区，设置了&quot;中国核试验爆炸地点&quot;、&quot;中国军用机场&quot;、&quot;北京周边军事区域&quot;、&quot;中国军事设施收集&quot;等八个版块，汇聚了各地网友发布的信息并进行分类整理。和普通军事论坛相比，月光论坛最显著的特点在于，它直接链接到国外一家地图网站的搜索引擎上。用户们可以通过客户端软件免费浏览全球各地的高清晰卫星图片，并在上面标注出军事地点的地理坐标和相关信息。<br />
<br />
　　通过网络上的追踪，执法人员找到了月光论坛网站的负责人小龙，通知他来配合调查。小龙是一个只有27岁的年轻人，目前在一家网络公司做程序员。<br />
<br />
　　小龙是一个地图爱好者兼军事爱好者。他承认自己提供了一个网络平台，对网友发布的军事信息进行分类和整理。但是对于执法人员所说的泄密问题，小龙不明白，人人上网都能看到的卫星影像，自己和网友们只是标注了一下坐标和信息，怎么就成了泄密了呢？<br />
<br />
　　国家测绘局副局长解释，网站上呈现出来的某一个区域的卫星影像甚至航空影像不能叫电子地图，因为它没有坐标，它也没有人文的、或者自然的、标注上的属性，但是一旦你在这个区域获取了一些应该保密的军事设施的地理坐标，它必然就会出现泄密。<br />
<br />
　　2010年5月6日，月光论坛的负责人小龙接受了执法部门的行政处罚，并承诺对网站进行整改。<br />
<br />
　　国内有42000家互联网地图网站 涉密程度越来越高<br />
<br />
　　月光论坛出现的问题并不是个例，随着电子地图技术的日益进步，各种电子地图和卫星影像地图软件也受到越来越多人的热爱。通常，网友们会通过电子地图软件查找交通路线、展示旅游图片、标注自己在当地的心得体会，但是，在卫星影像软件上可利用的内容还远远不止这些。<br />
<br />
　　25岁的龚夏威是国内第一家基于谷歌地球软件平台运行的网站--&quot;上帝之眼&quot;的创办人之一。龚夏威给记者演示了他网站的功能，他可以驾驶虚拟的小飞机以每小时600公里的速度飞过南宁上空。从南宁到北京，他飞了3个多小时，与实际飞行所用的时间、看到的地面景物几乎完全一样。<br />
<br />
　　他所负责的网站在圈内很有名气，拥有超过40万的注册会员。但是，近几年，他也一直被标注泄密的问题所困扰。网站创立之初，龚夏威从来没想过自己会和国家安全这么严肃的问题挂上钩，直到有一天，他亲眼看到有人在他的网站上贴上了一条涉嫌泄密的帖子，帖子说那个大院是国家×××部队，这个部队具体是怎么样的，并在谷歌地球上标注了出来，这个帖子赢得了相当高的点击率。<br />
<br />
　　与此同时，类似的论坛和网站在国内呈几何倍数增长，目前在国内已经达到42000家。被重点标注的目标包括军用机场、导弹阵地、雷达阵地、海军港口、部队驻地和一些重要的敏感目标等。<br />
<br />
　　某人在谷歌地球论坛上发布了某导弹基地的地标文件，就标注了该基地的准确位置和坐标。在有人发布的我国某雷达基地的帖子中，详细介绍了我国雷达的性能，并标注了我国两处雷达阵地的具体位置和布局。还有人在网络上发布中南海地标文件、西昌卫星发射中心地标文件。<br />
<br />
　　随着&quot;谷歌地球&quot;迷的不断增多，网上涉及我国军事机密的地标文件也越来越多，涉密程度也越来越高。2006年，有人标注了我国900兆瓦以上装机容量的200多座电站的具体位置。2007年，有人在月光论坛上发表了题为&quot;我军现役18个集团军领导及驻地&quot;的地标文件和&quot;西藏军区及驻西藏部队&quot;的地标文件。<br />
<br />
　　最初，管理网站运营的龚夏威只是将自己看到的涉密帖子删除了事，但随着注册用户的增加，这样的帖子越来越多。2007年，他委派同事邓庆生专门负责网络发帖的监管和审查。<br />
<br />
　　网友保密意识不强 无意中造成泄密<br />
<br />
　　据了解，如同现实社会有人炫耀权力、财富、美貌、甚至特异功能一样，电子地图的发烧友中也有人喜欢炫耀自己的独特发现以及一些不为人知的秘密，从而成为虚拟社会中的名人。<br />
<br />
　　据了解，有些发烧友甚至会不辞辛苦、实地勘察，帮一些网站、甚至一些敌对势力进行一些野外调绘工作。可能这些网友很多是无意识的，因此应该加强测绘、保密方面法律法规的宣传。<br />
<br />
　　根据《中华人民共和国刑法》有关条款：违反保守国家秘密法的规定，故意或者过失泄露国家秘密，情节特别严重的，处三年以上七年以下有期徒刑。故意或者过失泄露军事秘密，情节特别严重的，处五年以上十年以下有期徒刑。有关部门提醒广大网友，为了国家和民众的安全，请不要在公开地图上标示军事禁区、军事管理区及其内部的所有单位与设施；不得标示大型水利设施、电力设施、通信设施等涉及国家经济命脉，对人民生产、生活有重大影响的民用设施的具体形状及属性。特别提醒网友，也许您的行为无意中就泄露了国家机密，而&quot;泄露国家机密&quot;是要被追究刑事责任的。<br />
