百度参与国家测绘局互联网地图服务专业标准制定工作<br />
<br />
<br />
　日前，国家测绘局将于5月17日正式对外颁布中国互联网地图服务专业资质标准。该标准旨在对中国境内通过有线和无线网络面向公众提供地图服务的单位进行有效监管，以推动该产业的健康良性发展。国家测绘局方面表示，此次标准的编制过程广泛采纳了多方面专家、机构及企业的意见，在互联网地图服务方面深受用户欢迎的国内互联网领军企业百度也受邀参与了标准的制定工作，对互联网地图的服务质量、信息安全和技术等方面提供了诸多建议和支持。<br />
<br />
　　统计资料显示，相比于导航电子地图领域的烽烟四起，中国互联网地图服务市场同样竞争激烈。面对广大网民对地图搜索服务的需求，越来越多互联网企业开始将网上电子地图视为一个全新的关注点和服务内容。这种状况一方面促进了互联网地图服务产业的快速发展，另一方面也造成了电子地图服务提供商的良莠不齐。因此，规范电子地图市场，确保国家信息安全，提升网上电子地图服务的质量，也被国家测绘局提上了议事日程。<br />
<br />
　　据介绍，为了使标准真正满足市场的实际状况，并对互联网地图的发展形成促进，缔造中国互联网地图服务市场良性的产业氛围，国家测绘局广泛听取了各界专家以及以百度等为代表的诸多互联网企业的意见和建议，从服务范畴、质量管理、安全保密等多个层面出发，制订了一整套科学合理的电子地图资质审核标准。<br />
<br />
　　作为全球领先的中文搜索引擎，致力于为亿万网民提供便捷信息搜索服务的百度早早便将电子地图视为一项重要的服务内容。经历多次产品更新及技术升级之后，如今的百度地图已经作为百度的重量级产品登上首页，凭借便捷丰富的功能和良好的用户体验受到了近4亿网民的喜爱。伴随着世博会的开幕，近日百度还正式推出世博地图服务，凭借强大的三维地图技术、结合实时世博播报、立体场馆介绍等多种实用工具，打造出完美的百度地图三维世博园，为网友提供在线世博游览体验，深受用户好评。数周前，百度更是宣布免费开放地图API（应用程序接口），使国内数百万家中文网站都能够受益于百度地图所带来的便捷服务。<br />
<br />
　　据悉，此次国家测绘局的标准出台后，百度将积极依法申领互联网地图服务资质，接受国家有关部门的监督，而其即将最新发布的手机客户端地图产品，也将依照全新的标准进行开发和测试。<br />
<br />
