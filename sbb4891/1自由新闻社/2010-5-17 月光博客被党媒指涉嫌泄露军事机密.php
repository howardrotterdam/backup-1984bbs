关于月光论坛的一些说明<br />
<br />
在12天前的5月6日，深圳市规划和国土资源委员会打电话要我过去调查一下情况，他们说我的月光论坛网站上有涉嫌有包含“国家军事机密”的Google Earth地标文件，要求我进行删除。<br />
<br />
　　月光论坛（域名<a href="http://www.moon-bbs.com" target="_blank">www.moon-bbs.com</a>）是我在2006年用Discuz建立的一个关于Google Earth地标论坛，一些网友会在上面注册发帖，流量一直都很小，只有一个兼职版主管理，我有时候也去进行管理，该论坛最近两个月的总访问量为2607人，平均每天28人访问，Alexa流量基本为0，因为可以说，这个论坛并不是什么大型“军事爱好者”的论坛，因为其根本没有流量。<br />
<br />
　　下图是月光论坛的3个月流量统计，数据来源于Google分析。<br />
<br />
<img src="http://www.williamlong.info/upload/2180_2.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
下图月光论坛的Alexa统计。<br />
<br />
<img src="http://www.williamlong.info/upload/2180_3.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　月光论坛和月光博客不是一回事，论坛是论坛，每个人都可以注册发帖和上传地标，博客则不同，两者不能混为一谈，一边说着月光论坛，一边在画面上显示月光博客画面的做法会引起他人的歧义和误导。<br />
<br />
　　我个人并不是军事爱好者，我没有看过一本军事杂志，没有去过一个军事基地，对于军事知识的了解非常有限。<br />
<br />
　　关于月光论坛上网友上传军事地标的说法，目前相关的文章和文件已经删除，月光论坛也停止了用户注册，这是没有办法的事情。<br />
<br />
　　不过我还是认为，普通的网民随随便便在地图不太可能发现所谓的“军事机密”，一些“军事基地”本身就是禁区，普通人根本就进不去，根本无法拍照和标记，即使是在Google Earth卫星地图上，平头百姓也不太可能发现训练有素的军方卫星图片专家都没有发现的所谓“军事秘密”，因为一个卫星图片专家培训要10年以上的时间，“军事基地”本身也大多有各种隐蔽和伪装，平头老百姓用鼠标在网上点两下很难发现所谓“军事机密”。<br />
<br />
　　即使Google Earth上存在一些涉嫌“军事机密”的图片，我们也应该通过政府相关部门向Google施压力，要求Google限制了这些照片的分辨率，2005年的时候以色列政府就做到了这一点，以Google“不作恶”的信条，应该不会拒绝中国政府的要求。<br />
