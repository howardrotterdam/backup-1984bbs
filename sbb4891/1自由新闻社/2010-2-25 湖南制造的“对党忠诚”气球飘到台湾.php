这只来自大陆的空飘气球挂着“对党忠诚、对人民忠诚……”简体字标语，飘落在屏东县盐埔乡，居民见了觉得新鲜。图片来源：联合报<br />
<br />
<br />
中新网2月25日电 据台湾《联合报》报道，一只湖南省灯笼球制造商生产的大气球24日空飘到屏东县盐埔乡，气球上写着“对党忠诚、对人民忠诚”简体字，民众好奇围观这只飞越上千公里“投奔”台湾的气球。<br />
<br />
 <br />
<br />
“哇！好大的气球，还挂着简体字……”，昨清晨6时许，台湾矢崎公司清洁人员发现一只大型红色气球吊挂厂房上方，新围派出所获报，所长林伯芳商请乡公所调派工程车，费好大功夫才将气球拉下。<br />
<br />
气球上写着湖南省镇江中一广告制品公司制造、编号五十二号灯笼球，气球下方绑着红色布条，斗大简体字“对党忠诚、对人民忠诚、对事业忠诚、对自己忠诚”，布条随气球空飘上千公里，仍然完整。<br />
<br />
大陆制造商梁金花听到气球飞过海峡飘到台湾，一点不惊讶，“飞到台湾不只一次了”，她说，公司卖出去的灯笼球太多，多半用在节庆活动；分析可能是福建的客户购买，没有绑好才飘洋过海。</div>

							
							
							
							
