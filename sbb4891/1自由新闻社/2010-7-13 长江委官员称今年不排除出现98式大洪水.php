													<h2>长江上游大洪水形成中    洪峰流量将超1998年</h2>
						
						
																											<div id="postmessage_747372" class="t_msgfont">长江上游正形成大洪水&nbsp; &nbsp;洪峰流量将超1998年<br />
<br />
2010年07月18日16:22&nbsp;&nbsp;新华网<br />
<br />
 [导读]长江水文局最新预报，长江上游正在形成一场大洪水，洪峰流量将超过1998年，三峡水库将迎建成后最大规模洪水挑战，重庆港已禁止所有船舶通行。<br />
<br />
<img src="http://img1.gtimg.com/news/pics/hv1/171/207/576/37507356.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
7月17日下午，三峡大坝开启4个泄洪深孔和1个排漂孔，将出库流量由32000立方米/秒增大到34000立方米/秒以下泄洪水。新华社发（郑家裕 摄）<br />
<br />
新华网武汉7月18日电 （记者杨希伟）长江水文局最新预报，长江上游正在形成一场大洪水，洪峰流量将逼近70000立方米／秒，将超过1998年大洪水时的上游洪峰流量。<br />
<br />
长江水文局局长王俊说，受强降雨影响，长江上游干支流流量大幅增长，岷沱江、嘉陵江等河流来水持续快速增加，加上降雨仍将持续，预计长江上游洪峰流量将接近70000立方米／秒，将超过1998年大洪水时长江上游5万多立方米／秒的洪峰流量。<br />
<br />
据长江水文局实测，连日来长江上游区域持续降大到暴雨，17日嘉陵江旧院站日雨量高达225毫米。受强降雨影响，长江上游各主要支流普遍出现大洪水，支流洪水汇入长江后，长江干流寸滩站的流量持续增加，18日15时已增至42300立方米／秒。 7月18日长江水文局预报，三峡水库19日、20日、21日8时的入库流量分别将达到53000立方米／秒、66000立方米／秒、60000立方米／秒左右。由于上游多条支流洪水遭遇，预计22日金沙江还有较强降雨，三峡水库入库流量将直逼70000立方米／秒。<br />
<br />
长江防总办公室主任、长江水利委员会副主任魏山忠说，长江防总目前正密切关注长江流域的水情、汛情、工情与险情，积极采取科学、精细调度三峡水库拦蓄洪水等系列措施，以缓解长江中下游日益严峻的防汛形势。<br />
<br />
18日上午，长江防总会商认为，本次长江上游的洪水峰值虽然将超过1998年长江上游洪水的规模，但洪水的时间过程、总的洪水量不及1998年洪水，加上三峡水库的调控，将减轻中下游的防洪压力。 但魏山忠提醒说，由于近期长江中下游重要控制站城陵矶、九江、汉口、大通等站已超过或接近警戒水位。中下游地区受持续强降雨影响，长江干流、支流，沿江部分湖泊、水库的水位已较长时间处于高水位运行，堤防也是长时间浸泡，极易发生险情。降暴雨地区的内涝积水严重，渍灾也很突出。<br />
<br />
据长江水文局预计，本次上游洪水经三峡水库调蓄后，万里长江上最为险要的荆江河段水位将力争控制在警戒水位以下，洞庭湖与长江交汇处的城陵矾水位、武汉等长江中下游各站点的水位将突破或逼近警戒水位。<br />
<br />
三峡水库将迎建成后最大洪水 堪比98年最高峰值<br />
<br />
新华网北京7月17日电 16日到17日，湖北、安徽等地遭强降雨袭击，长江防总向长江流域各省市发出紧急通知，要求各地密切关注水雨情，及时启动应急响应，加强监测预报和水库、堤防等水利工程的巡查和除险。<br />
<br />
中国长江三峡集团公司预测未来2－3天内，三峡大坝将迎来一次峰值在65000立方米／秒左右的洪水，堪比1998年长江三峡河段的最高峰值，这也将是三峡水库建成以来所面临的规模最大的一次洪水挑战。同时，在三峡入库流量达到45000立方米／秒时，三峡船闸将停止通航。<br />
<br />
长江重庆段水位猛涨 重庆港禁止所有船舶通行<br />
<br />
中新网重庆7月18日电 (杜远 伍木林)为应对将于19日到来的洪峰、确保通航安全，重庆市海事局对辖区水域进行大面积交通管制，除少量特定船舶外，重庆港区等区域18日18时起禁止所有船舶航行。<br />
<br />
据重庆市海事局今天的通告，受四川、重庆地区连日大范围暴雨影响，嘉陵江、长江重庆段水位自17日以来连续猛涨，预计19日洪峰将陆续通过重庆主城区，长江重庆寸滩站19日17时最高洪峰水位将达27.15米，较18日08时上涨10.93米。<br />
<br />
为确保通航安全，重庆海事局将对辖区水域进行大面积的交通管制，白沙沱大桥、重庆港区、铜锣峡、黄草峡等水域从18日18时起，除防洪抢险船舶、公务船舶和海事部门特别允许船舶外，禁止所有船舶航行。此外，重庆涪陵以上水域禁止砂石船舶、汽车滚装船舶航行。<br />
<br />
据当地海事部门介绍，为应对洪峰过境对辖区航运安全的影响，海事部门已通过交管中心、水上安全信息台、甚高频电话、手机短信、GPS及巡航检查等方式将安全预警信息传递到了各有船有关单位。同时，加强了对重点港区、桥区、锚泊区、渡口的巡航检查。除此之外，局交管中心、辖区26个巡航救助执法大队、52艘海巡艇已进入24小时应急值班状态，并在重点水域指定了大功率拖船应急待命，确保如发生险情能及时有效救助。<br />
<br />
当地海事部门提醒，各船舶应谨防陡涨水引发的流木杂草碍航、航标失常；根据洪峰过境区段、时间和本船过滩、操纵能力，各船舶应及早选择安全水域停泊扎水。各船舶应加强与所航经水域的海事处及执法大队联系，服从海事部门的指挥。发生紧急情况可通过专用电话求助。</div>

							
							
							
							
