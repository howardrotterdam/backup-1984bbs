幽默科幻小说《蓝乌鸦的传说》(28)<br />
<br />
--------------------------------------------------------------------------------<br />
<br />
<a href="http://edu.sina.com.cn" target="_blank">http://edu.sina.com.cn</a> 2000/04/21 新浪文教 作者：周六棱<br />
<br />
<br />
　　在6月份，我比较热心抓泥鳅鱼，那时候，村子里的确热得要命，太阳在中午晒的更为猛烈，树叶也失去菜色，在呆呆的丝纹不动。我穿着小背心，中短裤来到村边的树林下，树上的蝉儿不停的喳喳叫，仿佛热呀热呀的呻吟，我仰起头，循着蝉儿声处望，太阳光从空中直射入目，使我把眼线眯成一字型，终于我发现了蝉儿，想捉它来玩玩，三下二下便爬了上去，不料刚要到树丫时，蝉儿被惊跑了，只好顺着树滑下。四周里没有人，我把食指伸进嘴里一吹。”啸──！”几声厉声哨响。”无聊！”我骂了一句，沿着小路出村口。<br />
<br />
　　我发现黄乌鸦从村口边走来，也是一件背心，一条中短裤，还剪了光头。我问：“去那儿？黄乌鸦！”黄乌鸦回答： “捉泥鳅、黄鳝去，你也去。”我问：“那儿能捉到鱼？”黄乌鸦说：“出去知道了，小渠有，水田也有。”我说：“我不会抓，你教我吧。”黄乌鸦说：“行，等一下你看我捉，很快学会了。”<br />
<br />
　　黄乌鸦拍了拍我的肩膀，俩人行出村子。来到田野上，好大的一片田野，大概因为太阳晒的厉害或是中午农人要收工回家吃饭的缘故吧，这片田野居然没有人，两兄弟都不戴帽，在如此烈日下游逛，真够可以的了，见头顶被晒烫了，黄乌鸦弯下腰来往水田要了点水往头上浇。<br />
<br />
　　黄乌鸦说：“水虽然很烫，但舒服，蓝乌鸦，你怕中暑的话，也学我一样把水浇上头发。”<br />
<br />
　　黄乌鸦又向我示范，我也仿着黄乌鸦一样做。我说：“痛快，象洗热水澡一样。”黄乌鸦催我：“你快点，咱俩是出来捉鱼的，不是来洗热水澡。”前面田埂边有一水洼处一片浑浊，黄乌鸦把鱼蒌交给我，然后告诉我：“那里肯定有泥鳅鱼。 ”我问：“你怎么知道？”<br />
<br />
　　黄乌鸦说：“泥鳅鱼怕热，象这样大的太阳晒，连水、泥都给晒烫了，它只好游出窝外打气泡，如果有人影晃动，它钻入泥中，使得它的周围水洼弄成一片黄浊。”我问：“这么灵吗？你去抓给我看。”<br />
<br />
　　两人行到水浊处，黄乌鸦不下田，只弯着腰，双手把水凹的泥浆捧上田埂，然后把泥浆分开，果然泥中露出两条蹦蹦跳跳的泥鳅鱼。我惊叹：“啊！真肥。”黄乌鸦问：“你这回会捉了吧。”我反问：“泥鳅都这么抓吗？”黄乌鸦说：“差不多。”<br />
<br />
　　我说：“好，我会抓泥鳅了。”黄乌鸦说：“你跟我来，我还要教你抓黄鳝。”<br />
<br />
　　俩人又行几步。黄乌鸦发现水田中有一个象花生米般大的窿洞，自信地指着那地方告诉我：“那个窿洞里面肯定有黄鳝鱼。”<br />
<br />
　　我问：“你怎么知道？”黄乌鸦说：“凭经验，怎么样？姜是老的辣吧。”黄乌鸦有点得意。<br />
<br />
　　我说：“你别吹牛，抓到鱼才算数。”黄乌鸦说：“你看吧。”黄乌鸦说完弯下腰来，只见他用食指往泥窿里伸进去，一会儿，一条大黄鳝鱼被他手中拧了出来，我把鱼蒌递过去，黄乌鸦把鱼往里装。<br />
<br />
　　他问：“看见了吧，捉黄鳝这么简单，碰上泥硬，便抠泥，只要挖到尽头，鱼便走头无路。”<br />
<br />
　　我说：“我也会抓了。”黄乌鸦说：“这样吧，你巡视一条田埂，我另外跑，咱分开走可以多捉到鱼。”黄乌鸦分任务。我说：“可以！”<br />
<br />
　　兄弟俩分着沿田埂走，我也学着黄乌鸦的方法，但很久却捉不着鱼，往往因为鱼灵活自己手笨，终还是逃脱，连叹霉气。前面又有一浑浊小洼，知道里面有鱼，怎么抓？想了很长时间，最后决定，在水洼外面挖一道宽宽的泥沟，把水洼和外面隔开，再抽干水，专心致致的干活，根本没有发现黄乌鸦已来到身边，他在田埂上望着我捉鱼。水抽干了，好，慢慢的把泥巴摊在手上，没有鱼，便扔掉泥巴，直到泥搬得得差不多，泥鳅鱼走头无路，被我双手捧上，装进鱼篓，我激动万分的喊：“抓到鱼啦，我……。”<br />
<br />
　　我抬起头，黄乌鸦在上面摇头叹气：“真笨！”我问：“那你抓到多少？”黄乌鸦把一串鱼往我面前一摆。我说：“ 噫哟，起码有一斤，等一下，咱俩去摘些香料回去，放多一点油，先把锅烧上火，然后爆它，绝对香。”<br />
<br />
　　黄乌鸦说：“最好有酒。”我问：“你饮酒？”黄乌鸦说：“学嘛。”我说：“我不学，酒是苦的。”<br />
<br />
　　黄乌鸦说：“这谁不知道！”我问：“既知道干吗还受罪。”黄乌鸦说：“不知道，你没有看见大人饮酒吗？瞧他们饮酒多过瘾呀，喂，不谈了，继续捉一阵子，咱们可要回去了。”<br />
<br />
　　俩人又分头走开。“噫哟！”我边走边叫，发现了跟前有一个大鱼窿，根据黄乌鸦指教的经验，判断是条大黄鳝。<br />
<br />
　　我说：“黄乌鸦，我见了大鱼窿。”黄乌鸦说：“抓它，照我的方法。”我说：“我担心它逃脱。”黄乌鸦说：“让我来吧。”他隔着一条田埂，便想过来。我说：“不用了，我可以，嗯。”<br />
<br />
　　我很想在黄乌鸦面前表表功。鱼窿挺大，水面清，看的真切，这回肯定是条大鱼。我弯下腰，把鱼篓放在田埂上，便用手指试探着伸入鱼窿挖泥，明显感觉到鱼窿里面有东西在活动，于是继续挖泥。<br />
<br />
　　我又激动地喊：“黄乌鸦，见鱼啦。”黄乌鸦问：“是大鱼吗？”我说：“不知道，我刚刚碰着它。”黄乌鸦在远处指点：“继续挖泥，这大鱼是要缩到老窝才抓着的。”<br />
<br />
　　我又把手指伸入鱼窿去试探，手指在里面给咬了下，钻心的疼，我不得不迅速把手指缩回来。我喊：“我被它咬了。 ”<br />
<br />
　　黄乌鸦点教：“你把手指继续伸进去，等它咬稳了再把手指提出来，鱼也一块出来了。”我说：“咬得太痛了，连血也流了，手指也破了。”黄乌鸦说：“怕什么！黄鳝鱼咬不痛，要不，让我来。”<br />
<br />
　　我说：“别，我来，我来。”我说着，又把手指伸进去。我又喊：“噫哟，又咬着了。”黄乌鸦喊：“把手指伸出来！”<br />
<br />
　　我赶紧缩出手指。“噫呀，我的天，是条花花绿绿的蛇，黄乌鸦，你快来救我呀。”我喊着狂奔往黄乌鸦边跑，黄乌鸦在远处看是泥蛇，他笑呵呵：“泥蛇无毒，不用怕，咬不死人。”<br />
<br />
　　我那听得进去，双手不停的扬着，泥蛇却偏偏咬得很紧，我边跑边哭：“黄乌鸦，救命呀。”到了黄乌鸦跟前时，泥蛇松了口，掉到附近的水渠中，悠悠的往草丛钻了一下便不见了，我心惊胆颤的喘着气，而黄乌鸦在旁边嘻嘻哈哈的乐个不停。<br />
<br />
　　我没好气的问：“笑什么！有什么好笑！”黄乌鸦说：“笑你胆小怕事，一条泥蛇吓成这样，不是男子汉。”我说： “你别说大话，换了你也会这样的。”<br />
<br />
　　黄乌鸦说：“哈哈，我见多了，不怕的，哈哈。”黄乌鸦又忍不住笑出声来，他笑痛了肚子，一不小心双脚歪下田中，这时水里游来一条大蚂蟥，红里带绿的爬上黄乌鸦的脚面，黄乌鸦仍在乐个不停，蚂蟥爬上小腿仍不知觉。我抬起头，发觉黄乌鸦的腿上流血。便惊叫：“黄乌鸦，你腿上有蚂蟥。”<br />
<br />
　　黄乌鸦说：“你别吓我，我是吓大的，不怕。”黄乌鸦心里说硬，说归说，他还是弯腰往腿里瞧，噫哟，好大的一条蚂蟥正吸着腿壁上，血顺着腿往下流，黄乌鸦手忙脚乱的跺着脚来，这条蚂蟥是不松口，牢牢地吸着，他又跳上田埂，用双手扯着。黄乌鸦骂：“真他妈腻味人，这恶心的东西。”<br />
<br />
　　他用手撕开蚂蟥头，蚂蟥尾紧紧吸着，撕开蚂蟥尾，蚂蟥头又紧紧吸着，黄乌鸦狂跳起来：“蓝乌鸦呀，你帮我除掉它呀！”黄乌鸦哭着喊。“蚂蟥嘛，没有毒，吃饱了，自然掉。”<br />
<br />
　　我学着黄乌鸦的话。“废话，让它吸饱血我会死的。”黄乌鸦边撕边靠近我：“你帮我除掉它，我的手够不着。”<br />
<br />
　　我弯下腰来，用力抓住蚂蟥，闭着眼睛，咬着牙喊：“一、二、三！”用力把蚂蟥撕成两段，血满手掌，黄乌鸦此时却不敢看地下。黄乌鸦问：“除了没有？”我说：“还没。”黄乌鸦说：“快点，快点，不要让它吃我这么多血。”<br />
<br />
　　他不停的跺脚。我说：“噫哟，这蚂蟥不肯离脚，干脆让它吃饱算了，据说它吃饱会睡觉的。”<br />
<br />
　　黄乌鸦说：“你以为它是猪呀，不成，你赶快弄死它。”我说：“这东西不肯离脚，干脆敲断这条腿不要算了。”黄乌鸦说：“这是我的腿，蠢材，没腿我怎么走路。”<br />
<br />
　　我笑了起来。黄乌鸦看腿下，蚂蟥已死多时了，黄乌鸦嘟哝：“骗人，不得好死。”我说：“我好心不得好报。”黄乌鸦说：“不抓鱼了，我们回家吧。”我说：“你说我胆小，你也胆小。”<br />
<br />
　　黄乌鸦说：“你才胆小，看你被蛇咬时，象疯牛一样狂奔三百里‘黄乌鸦，救我呀’到处喊。”黄乌鸦学着我的动作。我说：“某些人喊：‘蓝乌鸦呀！救命呀！’。”<br />
<br />
　　我也学着黄乌鸦的样子。黄乌鸦说：“我才跑多少！50米，你，100米。”我说：“喂，你跑50步也有资格笑我的100步？”黄乌鸦说：“这说明我的胆比你大一倍。”<br />
<br />
　　我听了在他后面吓唬：“黄乌鸦，你脚下有条蛇。”黄乌鸦紧张的跳起来：“呀！──我的妈呀，救命呀！”他又狂奔，恨不得爹娘多生两条腿。“哈哈哈。”我在后面掩面笑。</div>

							
							
							
							
