<br />
蔡真妮<br />
<br />
　　我在加拿大的小镇上开干洗店时，镇上的东方人除了两家中国人之外，还有三户韩国人。一家开餐馆，一家开杂货店，还有一家男的是科学家，在镇外的农科所工作。<br />
<br />
　　科学家的太太长了典型的韩国人的平脸，细长眼睛，年纪在五、六十岁上下。她经常到店里来干洗衣服，同是东方人，见面了会有很亲切的感觉，我看到她，总会和她聊一会儿，有时候她还把自家种的蔬菜送给我。她早已不工作了，儿女也已成家立业，闲来无事，总是到教会帮忙，做义工。<br />
<br />
　　她经常劝我到她们教堂去参加礼拜，认识主。和我在一起时，虽然一直是和蔼可亲的样子，可我发现她的眼睛里时时会闪过一些我琢磨不透的情绪。<br />
<br />
　　慢慢熟了一些以后，她问我家里父母是做什么的，兄弟姐妹是做什么的，后来又问有没有亲朋好友是当兵的等等，我感到她不是无缘无故问这些，却也没往心里去。<br />
<br />
　　后来我无意中问起她的家世，她的脸色就变了，抑郁中夹着悲伤，还有一丝丝――恨意。<br />
<br />
　　原来她家在南韩乡下是大户人家，大概就是我们说的大地主。父亲这一代有兄弟五人，结婚了三个，都住在一个大宅院里，全家人规规矩矩地过日子。朝鲜战争期间，他们那个地方被北朝鲜和中国军队占领了。<br />
<br />
　　一天深夜，她家里突然冲进来一些军人，把她爷爷和她爸爸还有两个伯伯一个叔叔强行带走了，妈妈搂着她和弟弟被荷枪实弹的军人逼在屋角，眼睁睁看着爸爸被带走，大气都不敢出一口。<br />
<br />
　　她说至今都记得爸爸临出门时回头望她们娘三个的那一眼。<br />
<br />
　　从此生不见人，死不见尸，留下满门孤儿寡母。<br />
<br />
　　全家的成年男丁中只有小叔叔当时在汉城读大学，逃过一劫。<br />
<br />
　　她当时七、八岁的样子，已经记事，失去亲人的伤痛刻骨铭心。<br />
<br />
　　我问了一句：“那你说的士兵是北朝鲜的？”她死死地盯着我：“是你们中国人！是中国军人！”<br />
<br />
　　然后她又说：“当时美国的飞机天天来轰炸，中国士兵把我们这些妇女儿童都赶到大街上，一排士兵一排老百姓地站着，然后向前移动。美国飞机盘旋再盘旋，无法扔炸弹，最后就只好飞走了。”<br />
<br />
　　我震惊于她讲的东西，半天说不上话。后来我说：“战争对于平民百姓而言都是残酷和恐怖的，你们当时的领导人为什么要侵略北朝鲜，发动那场战争呢？”<br />
<br />
　　她听了很吃惊，问我：“谁告诉你是我们发动的战争？是金日成的北朝鲜突然越过三八线攻入南朝鲜，占领了南朝鲜大部分的领土，联合国才派出部队在仁川登陆。再后来你们中国人就过来帮北朝鲜打联合国的部队！”<br />
<br />
　　我目瞪口呆。我学的历史不是这样的。<br />
<br />
　　也从来不知道在这个世界上有别的民族的人象我们仇视日本人一样地仇视着我们。<br />
