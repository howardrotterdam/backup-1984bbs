<br />
人们此前对上海世博的期望，正渐渐成为现实 <br />
<br />
随着天气升温，上海世博园的客流也不断攀升。开园两个月，总计有2100余万人次涌入世博园，这个数字已经逼近上一届爱知世博会6个月间2250万人次的参观者总数。<br />
<br />
那么，2100万人次走出世博园后，带走了什么？ 除了目不暇接的“西洋景”和造型奇形怪状的美妙场馆，几乎所有人至少都对两个词印象深刻：“排队”、“低碳”。<br />
<br />
中国人对世博的热情不仅让外方参展方吃惊，就连国人自己也颇为意外。尽管对世博的客流之众早有耳闻，游客的热情却不见消减。不管是在中国，还是在世界，这都是一个不折不扣的奇迹 排队七八个小时，不为果腹或任何物质欲望，只是为了看一场来自异邦的十几分钟的电影，只为片刻的精神享受和高科技洗礼。而且，这样的场景发生在一直被诟病没有排队习惯的中国人身上，试问，这还不足以感动世界吗？<br />
<br />
不妨对比一下两个多月前世博试运行时的场景：入园拥挤无序，入馆挤碎了玻璃，饮水池堆满垃圾，餐厅人满为患 网上以及外媒的负面评价不少。而那时最大客流量不过30万左右，现在的最高客流则已超过55万。尽管还有乱扔垃圾现象，还有困乏的游客席地而睡，还有种种不尽如人意，但是，井然的秩序、周到的服务、人性化的管理、志愿者的耐心，都开始一一落实。而游客的文明素质也明显提升，插队行为已成“过街老鼠”。<br />
<br />
还有一个醒目的词是“低碳”。园区的一切都在诠释这两个字。游客坐的运营车是新能源的，许多场馆的建筑、设计和展示都奔着“节能”，就连赴上海观世博的旅行方式，都会有人根据“碳排放”高低，帮你设计“绿色出行”计划 不管走到哪儿，“低碳”两个字都会顽强地冒出来，提醒你这届世博会的主旨。<br />
<br />
这两个词，一个代表国民文明素养，一个象征城市发展理念。如果，在2000万和以后更多的观博者中，能将这样的意念牢牢地印在心坎，这届世博就算没白办。也让人能够理解，为什么说日本大阪世博会的6000多万客流，助推了日本战后的起飞。看来，人们此前对上海世博的期望，正在渐渐成为现实<br />
<br />
<br />
上海世博会办得很辛苦，不说在市中心艰难地完成腾笼换鸟，硬是实现了产业结构调整与捧出精彩园区的“双赢”，也不说城市基础设施更新换代的神速，单说开园后面对每天涌入一个中等城市人口的巨大压力，这个城市就在殚精竭虑中收获了管理水准与协调能力的大幅提升。还有4个月，世博还将为上海、为中国带来什么？<br />
<br />
过去，我们会说，世博让上海沿江地区完成一次脱胎换骨式的改造升级，让上海城市交通发生脱胎换骨之变；现在，我们有理由说，世博这面镜子，还让上海、让中国看到更多的不足，让国人看到了在国民素质、创新意识和发展理念方面，我们还有更多的脱胎换骨的必要，而这些，才是世博即将留给中国最重要的精神成果。<br />
