2009-09-14<br />
<br />
大陆十一国庆之前，又再借“外出旅游”为名，牵制维权人士、异见作家的自由，不过今年文明了一点，连异见人士的妻子都可以陪同一起被“软禁”。有维权人士直斥强迫旅游的手法有如“黑社会”。（何山报道）<br />
<br />
<br />
大陆的维权人士、活跃网民，被公安、国保请喝茶、请吃饭时有发生，而在国庆大典之前，当局对不同声音人士的“礼遇”也升级，相关人士已经被告知，要有心理准备，离开原居住地，到外面“旅游”，异见人士称此不自愿的外出旅游为“被旅游”。<br />
<br />
独立中文作家笔会的朱欣欣，就是被国保告知，要准备“被旅游”的其中一个。朱欣欣周一晚对本台说：是石家庄市桥东区国保大队的副队长乔卫东，是在12号的下午6多，打我找去，他说想跟我打个招呼，说领导没有最后确定，但是以这个打算，想在国庆期间，出去“旅游”，所谓的旅游，就是像June Fourth期间，把我带走，到外地找一个旅游的地方，变相打我软禁起来。<br />
<br />
之前，本台也报道过，北京维权人士HuJia虽人已在狱中，但妻子金燕今年的国庆，也恐怕要被请去旅游，离开北京，HuJia非常担心。<br />
<br />
记者：你觉得这说成是取&quot;旅游&quot;与黑社会的做法有分别吗?<br />
朱欣欣：没有，完全没有分别，绝对是黑社会做法，在威胁，说甚么旅游，旅游是完全自愿的，被强迫去旅游，我们大陆上都有一个新的流行语，教“被旅游”，被如何如何，用“被”这个字，带表我们老百姓被强迫，强奸民意的意思。我觉得这个“被”字用得好，我现在也是成了“被旅游”。<br />
<br />
朱欣欣曾在河北电台新闻部任职编辑，期间积极参与89年June Fourth民运，之后一直被列在国安的黑名单。今年的June Fourth20周年前后，在6月2日至8日，就被当局请去附近的九华山庄温泉养生会馆，软禁7日。<br />
<br />
10月1日国庆，当局更加 “文明执法”，连朱欣欣的妻子都准备一起请去。朱欣欣的妻子张女士，对本台表示，单位对她的压力不厌其烦：因为June Fourth前，找他就走之后，我就不知到怎么会事，单位告诉我的，去旅游去，后来单位问了，他们才打电话告诉我，到外面去旅游。我们领导也说，让我跟他谈，但我说我这一个人，不是我可以说甚么。<br />
<br />
