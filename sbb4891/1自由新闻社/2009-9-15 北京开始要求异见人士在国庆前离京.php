<br />
　　国庆来临之际，中国全方位大倒退：网络控制加剧、对访民任意抓捕，甚至北京的小商贩也被拘留，所有异议人士要被赶出北京，而异议人士在外地也被严密跟踪、看管。同时，分析人士指出，中国表面繁荣，但实际上腐败严重，矛盾日益加剧，中共已经感到危机，<strong>预计这次政治倒退很可能不单纯是应付国庆，而是借国庆实施而已。</strong><br />
<br />
　　各门户网站受限，封网加剧<br />
<br />
　　博讯14日报道，国新办要求各网站在国庆期间，即便对于普通刑事案件的报道，也不能“掉以轻心”。从今天中国各门户网站主页内容看，果然刑事的都不见了。而且，博客、论坛的帖子，主页也一概不再显示题目了。消息人士认为，中国对互联网的控制，倒退程度让人震惊。<br />
<br />
　　近期，中国用户访问海外网站越来越困难，网络封锁明显加强。而博讯则在本月11日开始，连续几日受到DDOS攻击，攻击来访的频宽达到每秒600M字节（正常在10M左右）。<br />
<br />
　　北京清理访民，甚至连小商贩也拘留<br />
<br />
　　博讯从不同渠道了解到，中国借国庆之名，清理北京访民，严过奥运，消息称，各地官员借国庆的“尚方宝剑”任意抓捕访民，本次和奥运不同在于，这次显示中央和地方通力合作，一起抓捕访民。<br />
<br />
　　博讯据可靠消息来源了解到，北京对访民不但是送马家楼，很多都拘留。甚至北京街头的小商贩也抓捕到拘留所，上星期五一个晚上就抓了500多人，有上访的还有小商小贩。据称要过十一以后才放。<br />
<br />
　　异议人士要全部赶出北京，外地情况也糟糕<br />
<br />
　　异议人士正被赶离北京，其中包括一些维权律师。而在外地的异议人士，日子也不好过。近期长沙的谢福林兄弟被逮捕。博讯最新消息显示，杭州众多维权人士民主人士被当局安排人员24小时贴身紧跟。9月11日上午被系狱中的吕耿松夫人汪雪娥首先被街道、小区告知根据上级安排已开始派人对其进行24小时跟踪监视，要到国庆长假完毕结束监控，要求她外出必须向小区汇报。9月15日早晨，维权人士朱瑛娣女士家门口3人一班开始守候，并对朱瑛娣外出贴身紧跟。同日上午中国民主党浙江党人朱虞夫、毛庆祥胡晓玲夫妇，均开始享受当局安排多人守候监控待遇，民主党人祝正明则每天被辖区警察询问行踪。<br />
