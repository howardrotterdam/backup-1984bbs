													<h2>丁子霖：2010年6月3日夜晚木樨地路祭纪实</h2>
						
						
																											<div id="postmessage_658710" class="t_msgfont">丁子霖：2010年6月3日夜晚木樨地路祭纪实<br />
(博讯北京时间2010年6月04日 首发)<br />
&nbsp; &nbsp; 作者：丁子霖<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 我们一直等待着6月3日夜晚去木樨地桥头为死去的亲人祭奠。去年的这一天，我们被国安禁止，没有去成。今年，我们说什么都得去。但是，还没有等到6月，决定和我们一起去祭奠的杜东旭夫妇就“被邀请”到武汉干休所去“休养”了。还有一位徐珏女士，她今年得了癌症，而且正在进行第二次化疗，我们叫她不要去，她一定要去，她由她的小儿子陪同一起去。但是，到了6月3日夜晚，她那里的警察说什么都不让她母子两人去。十九岁中学生叶伟航的妈妈尹敏，早几天那里的警察就不让她单独离开家门了，说要到5号才解除。袁力的父亲已经94岁了，年迈体弱，目前正在住院治疗，他84岁的老伴行动不便，去不了了。其他亲属都是外地的，不能来。这样就剩下我们两位了。安全部门看我们态度坚决，没有来阻止我们，但提出的条件是：不打横幅，不“聚众”一起去，不接受境外媒体采访。我们本来就没有打算这样做。在6月3日夜晚，我们的亲人饮弹倒下的一刻，我们只想在那块血地上陪陪他们，给他们洒上酒，点上香烛，献上花，向他们说上几句话。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 6月3日夜晚10点一过，我和老伴便带上祭奠用品离家了。我们拒绝乘用国安提供的便车，自己找了辆出租车。国安的车就在后面随行。夜深了，路上车少人稀，我们十点半到达目的地――复兴门外大街29楼前、地铁木樨地站路北出口处。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 我们一眼望去，29楼东头与大马路之间已经拉上了警戒线，从东头到西头靠大马路又竖起了一长条大栅栏。线外、路边已经集聚了好几堆人群，我眼花缭乱一下子也没有认出几位热悉的记者，只听得日本NHK的田中先生在一旁轻轻叫了我一声“丁老师”，我默然点头回应。我们进入29楼前的空地时，身后又拉起了一条警戒线，把29楼前那一大片空地都围了起来，圈内有好几层便衣，圈外也有便衣、穿警服的警察、d红袖套的社区协勤员，以及外国记者，围观群众，足有好几十人混杂在一起，不时发生冲突，便衣粗暴地推搡外国记者，抗议声、叫骂声，此起彼伏。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 我和老伴默默不语，静静地坐在地铁站出口路沿上，等待着徐珏母子的到来。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 冲突越来越频繁，叫骂声不绝于耳。安全局负责人大概担心场面失控，要求我们提前祭奠，我说不行，说定11点开始的，我要等徐珏来后一起祭奠。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 5分钟过去了――这五分钟对便衣们来说也许够长的了。他让我与徐珏电话联系，开始我电话联系不上她，她家中电话和手机均无人接听。我感到事情不妙。果然，不一会儿，徐珏来电，说警方把她母子俩堵在家里，不准外出。她的情绪异常激动，在电话里可以听到她气愤地训斥身边的警员。徐珏重病在身，癌细胞已经扩散到肝部，是不能动气的。白天又刚做了化疗，我只得在电话里语不成句地连连劝慰她。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 我们不得不在10点45分提前举行了祭奠仪式。我和老伴拿出蜡烛、烛台、香炉和炷香、红白双色的玫瑰花，一瓶白酒，最后再拿出早先准备好的一条黑布条幅，上面平排贴着包括我儿子捷连在内的十位在木樨地遇难者的照片，每张照片下方都贴有他们的名字。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 本来我们在每张照片前方还分别摆上了小烛台，但当夜这里风大，刚点燃的蜡烛就被风吹灭了，好在我们准备了一对高大的插有蜡烛的烛台放在我儿子的遗像前。然后我们鞠躬行礼默哀。我手举酒瓶挨个儿给遇难者奠酒致哀：<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 她是马承芬，1989年她53岁。我说：“马承芬大姐，您是我找到的在木樨地遇难的年长者，他们都是您的晚辈，恳求你在另一个世界对他们都关心和照顾。……你的老伴被送到外地‘休养’去了，不能亲自来祭奠您，他们委托我了。”<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 第二位是肖波，北大化学系讲师，遇难时27岁，我说：“一个能长久被人怀念着的人是幸福的。你老母亲已经病故随你而去，如今你那远在湘西的老父亲已病重在床，无法圆他来京的梦了。他早先就托我前来为你祭奠，我一定要来的。你当时一对才70天的孪生子如今已是在校大学生了。”<br />
&nbsp; &nbsp; <br />
