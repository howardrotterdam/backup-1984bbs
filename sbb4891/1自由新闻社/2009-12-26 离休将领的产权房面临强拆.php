													<h2>离休将领的产权房面临强拆</h2>
						
						
																											<div id="postmessage_397968" class="t_msgfont">2009-12-24<br />
北京的一批离休军队高级干部的产权房目前正面临被强拆的命运，已经属于他们的私有住宅正被解放军总参一步步的侵吞，家属们在状告无门，国内媒体更不欲插手的情况下，希望引起海外各界的关注。自由亚洲电台记者方媛报道<br />
<br />
<br />
将被拆迁的是位于海淀区西四环五棵松路北八号院内的解放军总参谋部军训和兵种部北京第一干休所的住户，该院内共有七十六家部队住户，户主都是原师级，军级离休干部，据悉，开国将领吴克华，向守志，原总后勤部部长王克上将等都曾居于此地。<br />
 <br />
<br />
父亲是军级干部的萧女士表示，总参管理保障部以给老干部改善住房条件为由要占用这块地皮为在职干部盖楼，她周四对本台表示：因为北京部队大院里基本上饱和了，根本就没地方了，再要地方就是到远郊区去了，他们能愿意住到那里？他们完全是为自己打算，给自己找一个窝。<br />
 <br />
该块地皮上都是别墅型的洋房，面积在160至250多平方米的小二楼，自家门前都有院落，而这些房子早就被家属买回来了，有房产证，是属于私有财产，一旦有一天房子被拆迁了，属于他们的私有财产就永远回不来了。房子被拆后，只可获得经济适用房，这种房子不仅要买，七年还拿不到房产证。<br />
 <br />
萧女士说：下一步他们就骗大家说有房产证，根本不可能，办房产证第一手续要齐全，第二还得有购房的发票，下一步就是有产权证也不会给这些老干部了，因为老干部都走得差不多了，就剩一些遗属了，根本不把你们放在眼里。如果真给我们折钱，我们也不闹了，搬走到地方买房子能有产权证，否则在这里下一步是什么呀，有产权房变成没有产权房了，象我母亲一旦不在了，我们就是被清理的对象，以后他们让你走，你说这房子是你的，你根本拿不出任何手续来，他们的目的就是这个。<br />
 <br />
本台记者曾拨打主管拆迁军队干部的地方部门电话，但是无人接听。<br />
 <br />
由于是部队内部事情，告到地方说管不了，告到部队最后又回到侵吞地盘的总参。另一位父亲也是军级干部的陈女士表示：我们现在到哪里说都不行，人家就说你们是部队的，我们管不了。我们叫120过来，他们派战士把120挡在门口，政委在门口站着，他们都害怕不敢进来，到哪没有说理的地方。昨天，北京市有一个环境保障的12350的电话，我们院里的子女都纷纷打这个电话投诉，他们昨天就派人来了，说我们是部队的不让他们进，后来什么结果我们也不知道。<br />
 <br />
这些离休将领的家属也曾经找过媒体，但媒体记者虽然表示同情，但还是帮不上忙。萧女士说：今年上半年，新华社有个记者我电话跟他联系了，他就表示两方面不能介入，一是宗教一是军队，只要涉及到军队，什么媒体都不报道，虽然他也有看法，但是也不敢报道。大陆就是这样。<br />
 <br />
离休将领家属也曾聘请律师向总参等领导发出律师函，但所有的信函都如石沉大海。本台记者试图拨打律师电话，但律师以不便回复为由拒绝接听。<br />
 <br />
离休干部家属非常同情普通百姓，认为连他们高干子女，高干家属都被如此对待，普通拆迁老百姓能找谁说理，谈到近期不断有访民为强拆而自焚，他们认为这些人的极端行为完全是被政府所逼，甚至这些高干子女也曾想过要走极端，以引起社会关注。陈女士说：我觉得要看事态的发展，很多东西被激发到一定程度会爆发的，我们肯定要跟他们争到底，因为凭什么这么欺负人？<br />
 <br />
而目前，虽然没有到强拆的地步，但是，军干部步步紧逼，做思想工作，给压力，据家属介绍，就在11月17号，一名副师级离休干部马兰山在当天上午被做了“工作”后，傍晚六点就去世了。萧女士说，现在就是逼你就范。她说：前边拆了，下一步就要盖，离我们那里也就十米都不到，盖起一幢十八层的楼，那我们就会一点太阳都见不着，那怎么住？没法住就得走。</div>

							
							
							
							
