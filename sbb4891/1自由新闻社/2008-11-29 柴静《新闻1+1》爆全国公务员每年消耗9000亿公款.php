													<h2>900000000000</h2>
						
						
																											<div id="postmessage_28689" class="t_msgfont">窃自：<a href="http://groups.google.com/group/Googroup/browse_thread/thread/2488d87c002cfbfb?hl=zh-CN" target="_blank">http://groups.google.com/group/G ... 7c002cfbfb?hl=zh-CN</a><br />
<br />
主题：中国官员&quot;三公&quot;9000亿<br />
<br />
<br />
全国人大常委会办公厅研究室特约研究员王锡锌在中央电视台的《新闻1+1》栏目中披露，社会上有个说法，中国行政开支，仅公款吃喝、公费出国考察、公车的&quot;三公&quot;费就接近9000亿元人民币（下同），占总行政开支的30％。<br />
<br />
节目播出后，中国舆论和网民大感震惊，有网民惊呼9000亿元是&quot;天文数字&quot;，足以建造90个上海磁悬浮。<br />
<br />
也有网民计算，每名中国人每年得花700元左右供公务员公款吃喝、公费出国考察和享用公车。<br />
<br />
王锡锌提出数字时也让主持人柴静惊讶，她重复问道：&quot;您再说一遍，是多少？&quot;王锡锌再次肯定地说：&quot;公款接待、公费出国考察、公车，也就是'三公'，一年9000亿元，占这个（总）行政开支的30％。&quot;<br />
<br />
不过，王锡锌强调，这个数字理应由权威部门公布，然而一直没有这么做，因此社会上才会有各种的猜测。<br />
<br />
自网上日前曝光江西新余团和浙江温州团滥用公费出国后，中共政府部门的&quot;三公&quot;费再次备受各界关注，而王锡锌在央视节目中爆出&quot;天文数字&quot;后，舆论和网民的讨论更是到了白热化的地步。<br />
<br />
广州中山大学哲学系退休教授袁伟时在接受本报访问时说：&quot;这个数字（9000亿元）的确让人大吃一惊。中国公布的预算一直很笼统，而且预算给人大审查时，也明显是在走过场。我一直想了解这个数字究竟有多少，但一直都看不出来。&quot;<br />
<br />
他也认为，由权威部门把相关的统计数字公诸于世是最理想的，但对于是否真能尽快实现，他却没有信心，因为政府的财政一向都笼统计算，短时间内要透明化不容易。<br />
<br />
袁伟时也说，广东省在监控各省市的&quot;三公&quot;费方面是走在全国前列的，广东近年来召开人大会议时，经常会有人大代表质疑相关数字，因此广东省的&quot;三公&quot;费相对比较透明。<br />
<br />
一名广东官员在谈及&quot;三公&quot;费时说，他担心&quot;三公&quot;费的新闻若&quot;抄得过火&quot;，可能影响政府部门的提升。他说，出国考察学习其实是好的，但在目前的敏感时期，即使是正规的国外考察，各地政府也未必敢办，因为&quot;出国考察和滥用公费出国的罪名很多时候只是一线之差&quot;。<br />
<br />
他也说，公款吃喝、公车大多时候其实是政府的必要开支，舆论不应吹毛求疵。他举例说，为高级官员安排公车和司机是必要的，毕竟中国官员（即使是高级官员）的薪金不高，很难买得起轿车，而轿车恰恰是在接见外宾、参加国家高层会议时所需，因为搭乘出租车不够体面，而出租车往往也不能进入到重要政府机构和部门的范围里。<br />
<br />
另一方面，滥用公费出国的曝光浪潮至今仍未平息，《中国青年报》昨天再报道，另一个张家港市领导的八人太太团浮出水面，如无意外，20多万元的开支相信也是公费&quot;买单&quot;。<br />
<br />
在网上揭发江西新余团和浙江温州团的网民&quot;魑魅魍魉2009&quot;再向《中国青年报》记者爆料，他捡到的文件袋中，还有一个名为&quot;张家港八人贵宾团&quot;的旅游团队资料，团员全部为女性。<br />
<br />
&quot;魑魅魍魉2009&quot;之前原以为是个普通团，所以没有将它的资料也发布到网上，但后来却发现，在这个团的一份文件资料中，赫然写着这个团的&quot;客人性质&quot;是&quot;张家港市长夫人以及市政府领导夫人等&quot;，他由此怀疑这个团也存在公费旅游的情况。<br />
<br />
据报道，太太团10多天的行程包括前往温哥华的维多利亚岛、参观尼亚加拉大瀑布群、波士顿的哈佛大学和麻省理工学院、纽约联合国总部、自由女神、世贸大厦遗址、百老汇街及时代广场等。太太团的行程中也依次安排了华盛顿的白宫、赌城拉斯维加斯、大峡谷飞机游、洛杉矶的好莱坞、夏威夷的珍珠港等著名游览项目。<br />
<br />
从酒店报价来看，该团住得最便宜的是在波士顿，每晚95美元，住得最贵的是在纽约，每晚415美元，其余的则在这两者之间，比如温哥华的斯坦利公园海滨广场酒店，每晚210美元；洛杉矶的万豪酒店，每晚352美元。资料显示，&quot;一行8人赴美国加拿大考察团考察费用（全程免领队接待费用）共计为23万3745元&quot;。<br />
<br />
另外，据《中新网》报道，中国国家外国专家局网站上日前刊出一份《关于停止西北理工大学境外培训资格的通报》，《通报》说，该司对浙江温州团2008年2月赴美国培训团进行了查处。经查，邀请方向国内审批审核机构提供了虚假日程，而《通报》也明确指出，拉斯维加斯是当局严禁培训团组去往的城市。</div>

							
							
							
							
