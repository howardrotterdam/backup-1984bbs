													<h2>国家高层密约各界专家赴中南海授课</h2>
						
						
																											<div id="postmessage_439802" class="t_msgfont">国家高层密约各界专家赴中南海授课<br />
2010-01-19 03:30:09　来源: 第一财经日报(上海)　<br />
第一财经日报1月19日讯 这是一张非常特殊的课程表，62次课，领域涉及法治、经济、党建、历史、能源、文化、民生等各个领域。<br />
<br />
这是一群特殊的“学生”。据《高层讲坛》主编、海军大连舰艇学院原副院长崔常发撰文称，每次听课的除了中央政治局常委之外，还包括党中央、国务院相关机构的负责人及在京的各部委领导。<br />
<br />
根据《第一财经日报》采访的数位中南海授课专家介绍，听讲人数约在五六十位。这批特殊听众可以说是中国政治权力中心的精英人士。<br />
<br />
这也是一群特殊的“老师”。尽管在采访中，多位专家谦虚地表示，不要说去中南海上课，其实只是讲解。但无可置疑，授课名单上的125位专家全部是精心挑选出的各个领域的翘楚。<br />
<br />
仔细研读62次学习的课程表，可以很容易发现，几乎中国在行进中遇到的所有重大热点、难点问题在这里都有涉及。<br />
<br />
他们关注什么？<br />
<br />
21世纪初的中国，正在面临一场全球化浪潮。体现在62次学习中，学习主题中带有“世界”、“国外”和“全球”字眼的就有26次，占到42%。中央政治局通过集体学习广泛地吸取世界各国在社保、政府服务、金融管理、安全生产、城市化等各方面的经验教训，提高治国执政能力。<br />
<br />
中国经济发展尽管遭遇种种挑战，但依然势头迅猛，过去五年平均超过10%的GDP年增长率令世界瞩目。中国政府也一直注意改进执政能力、提高宏观经济调控水平。<br />
<br />
体现在中央政治局集体学习中，经济相关专题成为所有主题中数量最多的。根据本报对学习主题的初步统计，经济相关课题占到31次，占总学习主题的50%，其中既包括金融体制改革，也包括经济增长方式转变、结构调整、财税体制改革等重大话题。<br />
<br />
“法治”成为62次课程表中出现频率较高的关键词。根据本报统计，有7次集体学习围绕法治展开，包括首次集体学习的宪法专题、依法治国、依法执政以及物权法、知识产权法等内容。<br />
<br />
此外，教育、医疗、就业、社会保障等民生话题，气候变化、非典、奥运等热点话题也出现在课程表上，成为中央政治局集体高度关注的内容。<br />
<br />
话题与时机<br />
<br />
据新华社主办的《t望》周刊透露，中央政治局每一次集体学习的主题都是经过精心的整体设计，务求紧扣治国理政面临的重点、热点和难点问题，达到最佳的学习效果。<br />
<br />
根据本报记者的采访和授课专家的描述，目前授课主题的确定主要有两个途径：一类是高层领导根据自己的关注亲自点题，另一类是由中共中央办公厅相关课题组人员根据当前热点问题进行筛选并报批。<br />
<br />
在中国社科院欧洲研究所研究员周弘看来，她2009年5月22日在中南海怀仁堂进行的一堂讲解在时机选择上非常及时。当日，她与中国劳动保障科学研究院何平(博客)研究员一起，就“世界主要国家社会保障体系和我国社会保障体系建设”这个主题进行了讲解。<br />
<br />
周弘告诉本报记者，从时机来看，当时中国的改革已经发展到了关键时刻，工业化、城镇化加速，对社会保障的需求随之增加。另一方面，在金融危机之后，也需要通过健全社会保障来解除民众的后顾之忧，改善消费预期，支持经济复苏。<br />
<br />
她分析，从世界范围的经验来看，重大的社会保障制度改革或突破都发生在大规模的经济危机之后，比如美国的社保体系正是在1929年大危机之后开始提出，并于1935年正式建立。此外，为使中国经济持续健康地发展，也需要进一步缩小地区、城乡之间的差距，而以社会保障为主要形式的社会转移支付正好为缩小这样的差距提供了机会。<br />
<br />
“综合以上几个原因，我认为在2009年头几个月进行这个主题的学习和探讨是恰逢其时的。”周弘说。<br />
<br />
另一个例子是2002年12月26日的第十六届中央政治局集体学习，主题就是宪法。2010年1月13日，当时承担讲解重任的周叶中向本报记者分析了首次集体学习选定宪法的原因。<br />
<br />
他认为，一方面，中国改革开放当时已有20多年，市场经济的发展已取得较大成效，社会主义市场体系已经初步建立，各方面对于民主法制建设的需求更加突出；另一方面，我国法律体系和法律制度对改革开放的保障作用也已得到充分体现，进一步推进民主法制建设的重要性越来越显现出来。<br />
<br />
“2002年刚好是我国1982年宪法颁布实施20周年，12月4日胡锦涛总书记在人民大会堂发表了重要讲话，这个讲话在海内外产生了广泛影响，国际上对中国共产党人给予了充分肯定，这也是新的中央领导集体树立良好的国际形象的一个见证。”周叶中分析。<br />
<br />
除了紧扣当前热点，政治局集体学习还着眼长远，关注到中国经济社会发展的中长期问题。<br />
<br />
1月13日，五天前刚为政治局集体讲解的专家贾康接受本报记者采访时表示，此次进中南海讲解，印象最深的是领导层高度重视，既紧扣当前重大问题，也结合中长期的问题进行思考和探讨。<br />
<br />
“客观上是把两者形成结合，既是当前重大的问题，又跟中长期问题有内在的连接。”贾康说。<br />
<br />
贾康与高培勇此次讲解的题目是《世界主要国家财税体制和深化我国财税体制改革》。这个题目当中也体现出当前重大问题与中长期问题的连接。<br />
<br />
当前中国经济体制改革进入深水区，深化财税体制改革，完善公共财政体系，提高财政管理绩效已经成为眼前最需要加速推进的大事，而其进展和成效对于未来中国经济社会的健康发展亦影响重大。<br />
<br />
助力决策的智慧传递<br />
<br />
作为政府决策科学化、民主化的一个体现，中央政治局集体学习是一种“带有咨询性的学习”，其对政策的影响作用不可小觑。<br />
<br />
国务院发展研究中心中国发展研究基金会副秘书长赵树凯与华中师范大学徐勇教授在2006年11月30日为政治局集体学习做了题为《我国社会主义基层民主政治建设研究》的讲解。<br />
<br />
赵树凯认为，中央政治局集体学习是很重要的一个高层领导掌握研究界研究成果的渠道，每一次专题不同，但是基本可以代表各自领域的现状。<br />
<br />
“实质上这也是一个听取情况汇报的形式，对于决策的制定、思考现实问题等也是一种帮助。当然，这种形式和地方政府等汇报工作情况显然不同，它主要是帮助决策的。”赵树凯告诉本报记者。<br />
<br />
记者在采访中发现，许多主讲人很看重到中南海讲课，一个重要的原因就是这样的讲课可以使自己所学所研助力决策，从而使个人的学术思想有可能经由决策往实践传递。<br />
<br />
为政治局集体授课的多位专家关注到集体学习之后中国发生的变化。<br />
<br />
<br />
“政治局集体学习是一个信号，一个加速点。虽然社保改革工作一直都在进行，但是2009年政治局集体学习后改革速度明显加快。所以，这种学习还不只是简单的知识传递。”周弘说。<br />
<br />
由于中央政治局每次集体学习都具有很强的针对性和现实意义，因此海内外的观察人士已将中央政治局集体学习的内容作为透视中国动向的一个窗口。从讲课时机和主题确定，以及集体学习后已成惯例的总书记总结讲话，都向外界传达出丰富的信息。<br />
<br />
一位为政治局集体学习授过课的专家告诉记者，从新华社刊发的正式通稿上最高决策层的一些说法中，可以读出很多重要的信息。<br />
<br />
