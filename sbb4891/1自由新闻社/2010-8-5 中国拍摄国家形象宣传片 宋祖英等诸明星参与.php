													<h2>中国开始多管齐下全方位向国际展示“软实力”</h2>
						
						
																											<div id="postmessage_776027" class="t_msgfont">2010年08月09日01:54　来源：第一财经日报<br />
<br />
获授权拍摄“国家形象宣传片”的上海灵狮广告公司近来被媒体聚焦,公司董事长沈赞臣也变得格外低调。<br />
<br />
　　“最终的执行方案还在探讨之中,整个人物篇最终如何构架也在商讨中。”沈赞臣日前在接受《第一财经日报》记者采访时表示。<br />
<br />
对于中国广告圈内的著名人物沈赞臣来说,接拍“国家形象宣传片”是喜忧参半,喜的是能够从竞标的三四家知名广告公司中脱颖而出,忧的是需面对各方的压力,包括外界对内容创作的挑剔,还有媒体的围追堵截。<br />
<br />
　　据新华社的报道,此次先期启动的国家形象宣传片包括时长15分钟的专题长片和30秒广告短片。拍摄地点分布各地,30秒广告短片集中了中国体育界、演艺界、学术界、科技界、企业界等50多位享有国际盛誉的优秀人才,力图展现当代中国生机勃勃的国家形象。<br />
<br />
　　目前,国务院新闻办公室已正式开始了国家形象系列宣传片的拍摄工作。其中名人篇预计于今年9月完工,届时将在BBC、CNN等国际媒体上播放。15分钟的纪录片则将在外事活动等场合播放。<br />
<br />
　　事实上,不仅是拍摄“国家形象宣传片”,中国已开始多管齐下向国际展示“软实力”,一场自主掌握话语权的“战斗”正在打响。<br />
<br />
　　国家形象广告接力<br />
<br />
　　“这不是第一次从国家层面重视'国家形象宣传片’的拍摄,从奥运会之前的宣传片到《中国制造》,这次应该是一次延续,毕竟,当我们从'中国制造’走向'中国创造’的时候,要给国际社会传递一种'中国是怎样的’的信息。”上海师范大学人文与传播学院副院长金定海告诉本报记者。<br />
<br />
　　2008年,国务院新闻办公室同样也是以竞标方式完成了“中国国家形象”宣传片的制作,梅高（中国）公司最终拿下了这个项目的代理权。<br />
<br />
　　当年正是北京奥运年,在历经“境外火炬传递”的种种遭遇后,国人才意识到,国际社会对中国的误解究竟到了一种怎样的程度。奥运会开幕前,“中国形象”非但没有因为奥运会的举办而变得“大放异彩”,反而被国际媒体“集中诋毁”。<br />
<br />
　　梅高拍摄的片子以浙江小镇一排红灯笼下的小女孩故事为主线,将中国30年的发展贯穿其中,该片不仅在当年的一些外事活动中播出,也在CNN、BBC等国际媒体上播出,引起了很大的反响。<br />
<br />
　　“这应该算是国家层面第一次系统地去做'国家形象’的战略宣传。”金定海表示。<br />
<br />
　　2009年,由商务部主导制作了宣传“中国制造”国际形象的广告宣传片,这则广告着重打造与巩固“中国制造”在全球市场上的声誉。这则广告经过了半年多时间的运作过程,一些国际主流媒体如CNN等被商务部作为了重点投放对象。<br />
<br />
　　不过,中国的国家形象整体上依然不甚乐观。今年上半年,BBC在28个国家进行的民意调查中,中国的国家形象仅在巴基斯坦和一些非洲国家的受调查者心目中为正面,在西方尤其是欧洲民众中,中国的形象竟然普遍比较负面。<br />
<br />
　　“一个GDP排世界前列的国家、一个有着悠久文化历史传统的国家,如果没有鲜明的国家品牌形象,是非常可惜的。”在今年南宁国际广告大会上,包括梅高（中国）公司董事长高峻在内的广告界大腕发出这样的感慨。</div>

							
							
							
							
