&nbsp; &nbsp; 他记得，此前卫生部门曾就疑似无形体病给乡镇和村里的医生宣讲过，“不过也就是随便说说，发些宣传资料，我们都没当回事，谁知道这病这么要命。”<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 在患病和死亡人数都比较多的鲇鱼山乡下马河村，村医周世瑶的诊所门口，还贴着一张泛黄的《人粒细胞无形体病防控知识问答》。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 周世瑶说，这是去年秋冬之际，卫生防疫部门下发的宣传单。当时，连续两名村民的死亡，使下马河村成为疑似无形体病的防疫重点村。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 误诊为了牟利？<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 信阳加强相关培训，但误诊仍在发生，有村民质疑基层卫生院为让患者多吊盐水才误诊<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 今年5月24日，国家疾控中心派员到信阳，对商城、罗山、光山等5县85个乡镇的负责人进行培训，要求“及时发现、报告和调查”。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 7月9日，任务被层层分解，信阳要求其自市级医院到村诊所，必须加强对疑似无形体病的防治培训。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 杨富和商城县的所有村医都被召集，进行了培训。杨富说正因为参加了培训，才救了妻子的性命。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 培训要求村医，遇到发烧者，一定建议其去乡卫生院验血，血小板和白细胞低于人体正常值，且日渐锐减，确定为疑似无形体病个例，要建议去信阳154医院救治。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 今年6月，中国疾控中心应急办主任丁凡一行赶赴信阳，商讨对该病的应对策略。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 据信阳154医院感染科主任崔宁介绍，从去年起，该科已经治好了100多名感染无形体病的患者。“只要初期不被误诊耽搁的话，这病太好治了。”<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 但是，出于各种原因，延误治疗的病例还是在不断出现。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 商城县人民医院一位负责人说，农民想省钱，只好去找村医；而输液是村医赚钱的主要项目，不排除有村医为了赚钱，“先挂几天水再说”。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 调查中记者发现，也有村民质疑乡镇卫生院为赚钱，延误病患治疗。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 伏山乡枫树村55岁妇女雷呈华，便是其中一例。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 7月12日，村医余涛为雷呈华治病。两天，烧不见退。余涛联想到蜱虫咬死人的传闻，建议其去乡卫生院去验血。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 这是余涛遇到的第一次无形体病疑似病例，“以前也没人来村里宣传过这个”。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 在伏山乡卫生院，抽血化验后，医生告诉家属，雷患有急性肠炎，要住院。女儿丁保玉对此表示怀疑，“我母亲半年前曾在商城县人民医院做过肠炎检测，没有任何问题。”<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 4天后的7月18日，家属将雷转至县医院。雷被诊断为疑似无形体病，后又被送往信阳，在154医院，雷病亡。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; “医生说我们来得太晚了。”丁勇席含泪说，他们怀疑伏山乡卫生院将雷留治4天，可能为了赚取医疗费。他们去卫生院讨要验血单，被告之结果正常，且已被县医保部门收走。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 伏山乡卫生院院长张晶告诉记者，当时检查时，雷呈华的血小板和白细胞并无明显减少。她并解释说，“这种病人体征每天变化很快，可能是在后期变化了。”<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; “公布疫情，止息恐慌”<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 商城县村民因不了解疫情已产生恐慌情绪，信阳市、商城县卫生部门都不公布具体病例<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 虽然关于疑似无形体病的培训在村医中展开，但很多村民仍对此病感到陌生，并害怕。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 传言开始盛行。在商城县城区，数人告诉记者，蜱虫侵入公园，已经咬死了几个在草丛游玩的市民。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 记者走访商城县多个村庄发现，绝大多数村民至今都没有看到官方防治无形体病的宣传。在死者龚正成所在的鲇鱼山乡庙岗村杨桥组，一老者问记者，“什么时候能救救我们？”<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 他说，只要下地，就肯定会被蜱虫咬住。“我年纪大了，不怕，咬死就咬死了，不种地吃啥？”<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 有旁观者还建议，国家能否派飞机过来播撒杀虫剂，杀死蜱虫。这马上遭到反驳。“蜱虫都钻在地里，你撒药有啥用？”<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 目前，无论是信阳，还是商城县的卫生部门，都表示无法提供具体疑似病例的数目及患者名单。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 据信阳市卫生局一位内部人士透露，信阳市政府曾经研究过如何宣传该病。“今年4月份，市长专门听取了卫生系统的汇报，最后领导得出结论，在病原体和传播途径尚未弄明白的情况下，大规模宣传容易造成群众恐慌，产生不稳定因素。”<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 这位人士还称，信阳作为一个地级市，是否有权发布疫情，让有关领导难以把握。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 伏山乡主管政法的一位乡领导说，政府之所以没有公开蜱虫疫情，是维稳需要，“怕引起进一步恐慌，更何况病毒此时还未知”。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 而调查中，有村民向记者表示，政府若不公布确切疫情，反而容易产生社会恐慌。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 记者获得资料显示，仅2007年3月到9月的半年内，河南共报告无形体病例79起，死亡10例。其中信阳72例，死亡9例。南阳市桐柏县7例，死亡1例。商城县在这半年内，报告了4例。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 吴玉涛认为目前数据肯定超过资料上的。他估计，整个余集镇，从去年到现在至少有近百人被蜱虫咬伤。据吴所知，有3人因此病而死亡。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 伏山乡卫生院院长张晶拒绝透露，伏山乡蜱虫叮咬的疫情，但她表示，“被咬的人的确很多”。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 根据“无形病防治指南”的要求，发现一例疑似无形体病，要在24小时内，通过国家疾控中心网报系统，按乙、丙类传染病上报。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 张晶还说，目前对于乡卫生院诊断出的疑似病例，并未网络直报。理由是，“目前能检测的只是疑似病例，不知道是否该报。但已将情况向县卫生局汇报了。”<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 吴玉涛认为政府应该让更多人知道这只虫的真实情况，至少人们知道该如何防治。只有公布疫情，恐慌才不会蔓延。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 他告诉记者了这一幕：<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 今年6月，父亲吴德政出殡的那天，一个12岁的少年脖子上爬了一只小虫子，他的母亲发现后，尖叫一声。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 人群霎时乱作一团，都慌了。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 友情提醒<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; ●防虫 由于蜱虫主要栖息在草地、树林中，因此外出游玩时最好在暴露的皮肤上喷涂驱蚊液，尽量避免在野外长时间坐卧。注意做好个人防护，穿紧口、浅色、光滑的长袖衣服。蜱虫常会附着在人体的头皮、腰部、腋窝、腹股沟及脚踝下方等部位。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; ●除虫 如果发现蜱虫附着在身体上，应立即用镊子等工具将其除去。因蜱虫体上可能含有传染性病原体，所以不要直接用手将蜱虫摘除或是用手指将其捏碎。 (博讯 boxun.com)</div>

							
							
							
							
