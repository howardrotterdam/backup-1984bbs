																											<div id="postmessage_818706" class="t_msgfont">蜱也叫壁虱，信阳当地俗称草别子。蛰伏在浅山丘陵的草丛、植物上，爱躲在茶叶背面。不吸血时，有米粒大小，吸饱血液后，有指甲盖大]<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 河南商城县多名村民被蜱虫咬后死亡，国家疾控人员前往研究对策，该病称为无形体病，早发现可防可治<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 去年10月初，商城县鲇鱼山乡下马河村，68岁的季德芳患疑似无形体病死亡。在她出殡时，曾照顾过她的儿媳黄志菊因身体不适住了院。对症治疗后，黄志菊康复。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 在季德芳去世的20天后，她的远房亲戚、同村49岁的妇联主任罗林英也因无形体病死亡。她死的第二天，曾照料她的二嫂突发高烧住院，按无形体病治后康复。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 两家患者的家属均对记者说，她们在商城县人民医院陪护病患时，医生并未叮嘱过，那种病具有传染性。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 记者了解到，卫生部于2008年2月19日，曾向各级卫生部门下发《人粒细胞无形体病预防控制技术指南(试行)》(下简称“《指南》”）。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 其中指出，蜱虫咬的无形体病属于传染病，人对此病普遍易感，与危重患者有密切接触、直接接触病人血液等体液的医务人员或其陪护者，如不注意防护，也有感染的可能。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 商城县人民医院副院长王德强表示，知道该《指南》，但对于上述两家患者家属的说法予以否认。他说，医院对于蜱虫患者都是按照传染病要求防护的。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 虫毒误诊为“精神病”<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 去年商城已出现因蜱虫而死亡的病例，但许多村医仍不认识此病，诊断为感冒、脑炎等<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 谈起蜱虫，王德强并不像村民杨富等人那么紧张。他说，这种病的临床死亡率大约在2%―8%之间，死亡病例多发生于老年人。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 资料显示，1918年―1919年的美国大流感，死亡率最高时达到5%。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 据王介绍，从去年起，商城县感染无形体病的患者中，死亡病例增多，这也让卫生部门加强了培训力度。“关键是不要误诊。一旦误诊，出现并发症就很难救回来。”<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 而在记者调查中，误诊并不在少数。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 商城县探访的7起疑似病例死亡者，横跨去年夏天至今夏，初发病时首先找的都是村医，且都被诊断为感冒。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 其中，曾泽平被错误诊断的病种最多。在治疗中，她却先后被诊断为感冒、精神病和脑膜炎等。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 曾泽平住在商城县鲇鱼山水库边，去年夏天，她去拎猪食，忽然觉得很困。虽然在村医处被当做感冒，进行输液。几天后，因高烧，她开始胡言乱语，自称看到神鬼。家中请来“大仙”驱邪。但曾泽平每夜会疼醒一二十次。<br />
&nbsp; &nbsp; <br />
