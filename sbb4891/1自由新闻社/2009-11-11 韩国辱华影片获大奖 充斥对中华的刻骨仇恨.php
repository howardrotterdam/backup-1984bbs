<img src="http://i35.tinypic.com/30jpmxi.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
11月6日上午8时50分在首尔奥林匹克公园的奥林匹克大厅举行的第46届大钟奖电影节上，导演金裕珍（60岁）的《神机箭》荣获最佳电影奖。而《神机箭》是一部明显带有辱华倾向的影片。据网友评论“全篇从头至尾充斥着对整个中华的刻骨仇恨、侮辱，以及无理的领土要求。（环球网11月9日）<br />
<br />
韩国人伤害中国人民感情的事，可谓是司空见惯。这不，人家又拍电影伤害我们了。而且，这样的电影还获了奖，可见韩国官方是鼓励这种行为的。对于这种事件，除了中国网友表示表示愤怒外，中国的官方和传统媒体却大都选择缄默。<br />
<br />
我们中国人向来主张以和为贵，但是当国家主权和领土完整受到侵犯的时候，还谈以和为贵，就难免会让人怀疑中国人是不是怯懦了。现在中国的国际地位今非昔比，无论是经济实力还是综合国力都有了很大提升。笔者认为，对于国际上的辱华事件，现在到有力反击的时候了。<br />
<br />
今天我只针对韩国，历数近几年韩国人的过分行径,“申请端午祭为非物质文化遗产”、“宣称孔子是韩国人”、“亚冬会事件”…<br />
<br />
这些事件无一不是极具挑战性的行为，严重伤害了中国人的感情。有网友讽刺韩国人无耻，认为韩国人的所作所为仅仅是因为无耻。笔者认为，网友的回应很无力，而且还显得我们中国人很浅薄。其实，这些事件背后，隐藏着很深的社会原因和历史原因。这些辱华事件集中的折射出韩国人集体性的自卑心理。<br />
<br />
为什么这么说呢？<br />
<br />
中国和韩国同处儒家文化圈，无论是在地缘上还是文化上都有很大的接近性。历史上朝鲜半岛在很长时间，不是被中华民族统治，就是纳贡称臣。（包括中国的少数民族政权元、金、清时期）所以，客观上讲，韩国在历史上也是饱受中国人压迫。二战后，随着民族独立和经济的起飞，韩国的国际地位提高很快，韩国人的民族独立意识不断增强。韩国开始追求国际大国地位。<br />
<br />
众所周知，两个短板是韩国人通往大国之路的最大障碍。一是，领土狭小；二是，文化贫瘠。并且，这两个因素都是致命的，也是短期之内不能解决的。可能是出于急切成为大国国民的心理，韩国人开始意淫中国文化和领土。从某种程度上讲，处于中日两个大国之间并饱受其蹂躏的韩国急于自强的心理是可以理解的，但是，以伤害他国人民感情为代价的意淫却是让人不赞成的。<br />
<br />
无论韩国人如何叫嚣，都改变不了孔子是中国人、长白山是中国领土的事实。但是，我们还是要坚决回应韩国的这种行为，勇敢的发出中国声音。关于主权问题是丝毫不能含糊的。<br />
<br />
我们不能让韩国的谎言肆虐，我们却无动于衷，那样中国将在国际上处于被动。拿《神机箭》这个电影来说，如果韩国凭借其强大的经济实力，拿到世界各地去放映后果将不堪设想。<br />
