													<h2>蔡：中华民国是流亡政府　府：走扁路线选举操弄认同？</h2>
						
						
																											<div id="postmessage_640498" class="t_msgfont">蔡：中华民国是流亡政府　府：走扁路线选举操弄认同？(2010/05/26 12:44)<br />
nownews<br />
 <br />
宣布要竞选新北市长的蔡英文，25日说出中华民国政府是流亡政府的言论?! <br />
&nbsp; &nbsp;<br />
记者王宗铭／台北报导 <br />
<br />
民进党主席蔡英文昨日发表「中华民国是流亡政府」的谈话，总统府发言人罗智强今(26)日上午质疑表示，前总统陈水扁执政时逢选举就会操弄国家认同问题，难道蔡英文成为参选人后就忘了自己的理性温和诉求，也决定走陈水扁路线，再次以炒作族群、操弄认同作为选举主轴吗？ <br />
<br />
罗智强上午受访表示，这是蔡英文对中华民国首次明确表示立场，却选择向深绿靠拢，令人非常遗憾。 <br />
<br />
罗智强说，台湾发展迄今，朝野好不容易拥有共识，就是以认同中华民国作为公约数，却因为蔡英文这一句「中华民国是流亡政府」，使得好不容易建立的国家认同因此倒退，令人失望。 <br />
<br />
罗智强说，中华民国自成立时即为主权国家，在中华民国的领土上存在发展了99年，蔡英文贬抑中华民国为流亡政府，是自贬国格，否定国家的主权独立，这种否定国家、否定宪法的态度也同时矮化了曾经执政的民进党，这是严重失言。 <br />
<br />
罗智强说，所谓流亡政府是指流亡至外国的临时组织，「请问蔡主席，台、澎、金、马是哪一个外国的领土」？难道民进党政府执政8 年，都是「非法无效」的治理吗？难道蔡英文的「10年政纲」，只是流亡政府的执政纲领？ <br />
<br />
罗智强说，民进党的「台湾前途决议文」也明白指出，「台湾是主权独立国家，宪法上的名称为中华民国」，难道蔡英文连台湾前途决议文都要否定？蔡英文要不要把「中华民国是流亡政府」纳入决议文？</div>

							
							
							
							
