<br />
至于江启臣则是表示，「中华民国是主权独立的国家，她存在99年了，也历经自由化、民主化，甚至政党轮替，所以中华民国的存在是不需要质疑的，反而我们要多加努力，透过政府与民间努力，促进族群融合、社会和谐」。</div>

							
							
							
							
