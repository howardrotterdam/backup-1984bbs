																											<div id="postmessage_639878" class="t_msgfont">有关台湾定位与主体性争议，民进党主席蔡英文昨首度公开表明，中华民国是一个流亡政府，在（过去）台湾统治的几十年来，看到威权统治与中国性的合体，如今中国性与台湾性、台湾主体意识发生微妙关系，以前中国性是主体，台湾性是客体，如今主客易位。<br />
<br />
&nbsp; &nbsp; 蔡英文强调，值此主客易位的关键时刻，必须处理更深层的问题，例如族群关系，如果未能认真面对外省族群在台湾本土化经验或他们的感情与认同，下阶段可能就要面对相互对抗与社会对立。<br />
<br />
&nbsp; &nbsp; 她还指出，如果把六十年来中华民国统治台湾的宰制性慢慢去除，把中国性回归人文的一种价值，回归其原始客体而非主体地位，则更能平心静气、深层思考大家共同往前走的这条路上，“我们如何来建构共同的国家、共同的新的台湾，这是现阶段非常沉重、也是非常重要的责任”。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;对蔡英文指中华民国是流亡政府，国民党文传会主委苏俊宾回应，质疑民进党到底认不认同中华民国政权？如果不认同，过去八年民进党执政是什么政权？蔡英文和民进党都要面对自己逻辑不一致的问题。民进党如果真有心解决族群的问题，就不该纵容党籍干部和民代撕裂族群的言论。<br />
<br />
&nbsp; &nbsp; 行政院也回应说，中华民国是主权独立的国家，经过自由化、民主化与政党轮替，存在九十九个年头。需要更多的包容与共识，让政府与人民持续携手前进，以促进族群和谐。<br />
<br />
&nbsp; &nbsp; 台教会昨举办“中华民国流亡台湾六十年暨战后台湾国际处境新书发表会”，台教会会长陈仪深、学者薛化元、黄智慧等都出席。<br />
<br />
&nbsp; &nbsp; 蔡英文透露，她曾想做历史学家，后来学法律就变成法律学者，她现在是政治工作者，“我知道，我不能用法律学者或历史学家的角度来看待中华民国的问题”，但这个问题是大家必须面对的，尤其在台湾主体意识不断成长的今日，台湾意识已成为主流思考。<br />
<br />
&nbsp; &nbsp; 她表示，必须在成长中的台湾意识来看中华民国这个问题。中华民国是一个流亡政府，在统治的几十年来，看到威权统治与中国性的合体，中华民国在台湾的统治，其威权体制性与中国性，让中国文化、中国语言在台湾都变成强势文化与语言。<br />
<br />
&nbsp; &nbsp; 她表示，二○○四年总统大选后，台湾主体意识高涨，民进党当时就看到了这个问题，因此通过一项“族群多元、国家一体”决议文，决议文体现对多元文化、宪政民主体制的价值追求，也表达对外省族群历史经验的理解。</div>

							
							
							
							
