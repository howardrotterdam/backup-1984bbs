													<h2>薛明凯犯有 “颠覆国家政权罪”吗？</h2>
						
						
																											<div id="postmessage_374300" class="t_msgfont"><a href="http://www.rfi.fr/actucn/articles/120/article_18119.asp" target="_blank">http://www.rfi.fr/actucn/articles/120/article_18119.asp</a><br />
<br />
作者&nbsp;&nbsp;马丁<br />
<br />
发表日期 15/12/2009 更新日期 15/12/2009&nbsp;&nbsp;00:34 TU<br />
<br />
山东青年薛明凯被控“颠覆国家政权罪”案，12月14日在广东深圳中级法院开庭审理，广东精神病司法鉴定所认为薛明凯符合无精神病的标准，律师表示他可能被判重刑。<br />
<br />
这是薛明凯案子9月10日第一次开庭后，第二次庭审。14日的庭审只是宣布对薛明凯的精神病鉴定结果。广东法医精神病司法鉴定所认为，薛明凯没有精神病。这意味着薛明凯将被当局以“颠覆国家政权罪”审判，并且可能被判重刑。<br />
<br />
当天，湖北民生观察工作室负责人刘飞跃告诉中央社，法院改变计划突然开庭，律师仅在开庭前3天才接到通知。<br />
<br />
薛明凯的代理律师是来自山东鲁兴律师事务所的律师孔祥荣。孔祥荣是11日才接到深圳市中级法院14日开庭通知的。接到通知后，律师和薛明凯的母亲王女士第二天晚上匆匆赶到了深圳。昨天上午，律师只能争取到看守所会见薛明凯，其他案情都来不及准备。<br />
<br />
薛明凯是山东曲阜人，今年20岁，之前在深圳当保安。薛明凯是今年5月9日被深圳市公安局宝安分局刑事拘留的，6月15日被深圳市宝安区检察院批准逮捕。他的罪名是涉嫌“颠覆国家政权罪”。检方指控薛明凯今年2月与海外民主党组织谢万军联系，并于4月12日被批准加入民主党。检方还指控薛明凯2006年7、 8月间在网上与友人商量成立“中国民主工民党”未果。<br />
<br />
从以上检方指控薛明凯的罪名来看，检方首先在法律上有混淆可受控年龄之处。检方将薛明凯2006年7、8月间在网上与友人商量成立“中国民主工民党”未果一事列为罪证，姑且不论中国公民筹组“中国民主工民党”社团是否违宪，当时薛明凯仅 17岁，还未到可以承担责任的法定年龄。至于薛明凯今年2月与海外民主党组织联系并被批准加入民主党，律师认为这与薛明凯的精神状况有关。<br />
<br />
薛明凯的辩护律师孔祥荣在此案的第一次开庭辩护词中，就强烈地认为薛明凯有精神病。他指出，早在2006年7月19日，薛明凯在山东省济宁市复退军人精神病医院诊断为患有重度抑郁症，还有精神分裂的特征。<br />
<br />
广东法医精神病司法鉴定所是在什么样的条件下作出对薛明凯的鉴定，外界并不清楚。众所周知，中国的法医鉴定机构从来不是独立机构。而深圳司法当局对薛明凯的审判，不仅检方混淆被告人涉案责任年龄，有违司法程序，而且这个案子很可能成为外界判断中国司法重在政治审判的一个案例。凡涉及组建政治团体等高度敏感问题的人，无论其个人情由如何，无论其涉案时精神状况是否可以承担责任，都将排除一切其他因素，将其定为政治案件。薛明凯可能成为中国政治审判的又一人。<br />
<br />
据湖北民生观察工作室负责人刘飞跃说，薛明凯的母亲告诉他，2006年4月，薛明凯才17岁，还在读书时，在学校的教室内被打伤头部，随后患有重度抑郁症，随即离家出走，两个月后，家人才从外面把他找回来。找回时，薛明凯浑身不像个样子。今年9月10日第一次开庭后，薛明凯家人和律师即为薛明凯申请了精神病鉴定，法院对此也予以接受。<br />
<br />
但在昨天的庭审上，法庭宣布了对薛明凯的精神病鉴定结果，广东精卫法医精神病司法鉴定所认为，薛明凯符合无精神病的标准。这意味着薛明凯将被当局以“颠覆国家政权罪”审判。报道说，昨天的开庭，是从下午3点开始的，只持续了20钟分庭审就结束了。接下来案情如何，薛明凯的个人命运如何，几乎已经是定案了。<br />
<br />
法院认定薛明凯无精神病状况后，律师认为，依“颠覆国家政权”这一罪名，薛明凯被判重刑几乎已经不可避免。一个曾经有过政治理想，但头部被打伤，患有抑郁症的青年，一个曾经被山东省济宁市复退军人精神病医院诊断为患有重度抑郁症，并有精神分裂特征的青年，得不到医疗和关怀，反而成为思想犯，在一个政权高于人权的国家，还和人们记忆犹新的极权高压时代无所不在的“反革命现行罪”一样，因为头脑中的一个政治想象，就必须成为犯有“颠覆国家政权罪”而被送进监狱的一名犯人。</div>

							
							
							
							
