													<h2>山东青年网上加入民主党被控颠覆罪</h2>
						
						
																											<div id="postmessage_306117" class="t_msgfont">2009-10-29<br />
山东青年薛明凯因今年4月在互联网加入海外政治组织中国民主党，今年9月被深圳法院以“颠覆国家政权罪”开庭审理。薛明凯的母亲王女士星期四对本台表示，儿子只是在网络上加入海外组织，并没有实际参与活动。律师为他做了无罪辩护。<br />
<br />
2006年曾在山东兖州第一中学读书的薛明凯，今年四月因在网络上发表批评政府的文章以及加入海外民运组织中国民主党，随后被深圳宝安区公安拘捕，九月被控以“颠覆国家政权罪”，案件在深圳法院开庭。薛明凯的母亲王女士星期四对本台表示，起诉书指控他儿子加入境外敌对势力：“起诉书上指控零六年七、八月间，在网上散布对共产党不满的谣言，联系张军（音）、吴思陆（音），他啥都没见过面，将组建民主公民党，从来没建成，到了零九年说他联系在美国的中国民主党主席谢万军，4月12号加入到这个民主党。”<br />
 <br />
王女士表示，儿子于今年4月12号在网上加入中国民主党：“不知道谢万军的民主党是（当局所指的）敌对组织的，5月8号逮捕的”。<br />
 <br />
本台尝试联系1999年4月逃出中国，经俄罗斯逃亡美国的中国民运人士谢万军，但没有联系上。<br />
 <br />
据中国维权网站民生观察工作室星期四报道，现住在深圳市宝安区的薛明凯，6月15日被深圳市宝安区检察院批准逮捕。9月10日，案件在深圳市中级法院开庭。有深圳异议人士近日被警方警告不得关注此案。<br />
 <br />
薛明凯的辩护人孔律师表示，检方的指控是错误的，因此他为被告做了无罪辩护，他称，颠覆罪必须具备以下条件：“他必须组织人力、物力、财力，他要有队伍、有装备，才能篡夺国家政权，他不够篡夺国家政权的条件，所以起诉他犯罪是不对的，他的（犯罪）主体、客体都不够”。孔律师表示，被告是在网络上入党，也未见过民主党人：“他也没见过谢万军，说他那个组织是敌对组织，你又没有证据，既然没有证据，你就不能认定他加入了敌对组织的党，也不能认定他犯罪”。<br />
 <br />
今年20岁的薛明凯在三年前已被迫离校。王女士说，薛明凯于2006年4月5日在学校的教室内被同学打伤头部，后患重度抑郁症。随后离家出走，6月7日，她和家人才从外面将其找回来。找回时薛明凯浑身不像样子。在法庭上，律师已提出为薛明凯申请精神病鉴定，正等待鉴定结果。<br />
 <br />
正在深圳为儿子奔波的王女士称：“广州精神病院正在做精神病法医鉴定，做出鉴定之后，再开一次庭”。而在此之前，王女士多次到北京上访，但被当地政府关押。<br />
 <br />
对于起诉书指薛明凯发表文章批评共产党，孔律师说：“对共产党有意见还是因为班主任对他不公，因为他在学校时，无缘无故地遭到同学用板凳砸破头，班主任不但不批评砸他的学生，反而因为他头痛发烧，不能上体育课，就不让他上课了”。<br />
 <br />
孔律师表示，薛明凯根本不知道官方对海外中国民主党的定性，他什么工作都没有做过：“自己写了申请书，还编了一个零八宪章，他编了以后准备到国家信访局去商量，反映他的申诉书，向信访局反映情况”。<br />
 <br />
以上是自由亚洲电台特约记者乔龙的采访报道。</div>

							
							
							
							
