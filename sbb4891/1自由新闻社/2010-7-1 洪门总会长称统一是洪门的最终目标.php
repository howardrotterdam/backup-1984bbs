&nbsp;&nbsp;<br />
　　中国台湾网4月5日消息　据台媒报道，台湾洪门兄弟昨天上午在台南县柳营乡果毅村举行追思始祖活动，在郑成功军师陈永华的墓地前，摆开阵仗，由全球洪门联盟总会会长刘会进主祭，洪门子弟500多人参加。&nbsp;&nbsp;<br />
<br />
　　台湾洪门认为，金庸武侠小说《鹿鼎记》中的陈近南就是他们洪门的始祖，而且就是郑成功的军师陈永华。&nbsp;&nbsp;<br />
<br />
　　逾300年，洪门创始人陈近南的身世之谜，洪门认为已经有了定论，他们认为陈永华就是陈近南，洪门正筹建陈近南纪念馆，希望未来能发展成观光景点。&nbsp;&nbsp;<br />
<br />
　　洪门总会长刘会进表示，当年精通孙子兵法的陈永华，随郑成功来台湾并辅佐郑经，而郑成功派陈永华回大陆时，陈永华就化名陈近南。&nbsp;&nbsp;<br />
<br />
　　而他们现在所拜之墓底下埋的并不是陈永华的骨骸，只是衣冠。刘会进指出，因为当时抓到“反清复明”，是要抄家灭门的，所以陈永华的骨骸现在在哪里，他们并不清楚。&nbsp;&nbsp;<br />
<br />
 　台湾洪门总会负责人刘沛勋先前曾指出，三百多年历史的洪门一直披着神秘面纱，给外界许多想象空间，其实洪门是近代史上一个自发性的民族革命团体，满清时代的洪门打着“反清复明”的鲜明旗帜，从事地下革命活动，成为满清皇朝想除之而后快的团体，为求自保，洪门始终以不同名称出现，如：天地会、三点会、哥老会等，并以神秘的言谈手势对外活动。&nbsp;&nbsp;<br />
<br />
 　刘沛勋表示，国民党当局入台后，洪门处于被禁止活动的情况，直到解严后，约有五十多个洪门山堂，联合组成“社会事业建设促进会”作为洪门总会对外窗口，并得到台当局核准，可在台湾公开活动举行各种仪式，只是“洪门”二字始终不能出现。&nbsp;&nbsp;<br />
<br />
　　经过多次申请，2003年终获得同意筹组洪门总会，并在去年一月召开成立大会，获得台当局正式立案证书，洪门由地下到地上正式走上台面。为减少外界疑虑，新的洪门团队明订“排黑条款”，只要是有案底、前科或兄弟色彩者，皆不得入会。刘沛勋指出，有别于传统革命性、刚性、神秘性的洪门，新的洪门将转为社会性、柔性，以反毒、照顾老人、妇女等社会关怀为诉求，积极走入社会，朝慈善社团发展。&nbsp;&nbsp;<br />
<br />
　　昨天祭拜典礼于上午11时开始，整个祭拜仪式以正统洪门规矩进行，站班护卫以迎凤接驾仗式迎接贵宾，整个仪式近一个小时才结束。<br />
