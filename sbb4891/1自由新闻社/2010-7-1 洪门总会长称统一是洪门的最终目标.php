<br />
　　刘会进目前担任中华全球洪门联盟总会长、中国洪门五圣山山主、中华海峡两岸文经互利协进会理事长、安徽合肥神行太保文武学校名誉校长、洪门国武术协会名誉理事长，经常往来两岸，为两岸和平交流而努力。<br />
<br />
　　刘会进表示，“洪门”起源于明末清初1674年，至今已经有336年历史，洪门自始至终强调“忠、义”、维护中华民族，在台湾有20多万人洪门子弟，2004年10月在台湾正式登记立案为“中华全球洪门联盟”，由“立法院长”王金平担任名誉总会长。<br />
<br />
　　他指出，在大陆，洪门是“致公党”，致公党党主席是全国政协副主席、中国科技部部长万钢，在其他国家有华人的地方也都有“洪门”组织，“洪门”昆仲散布在世界各地共千万人，“洪门”对中国大陆和世界的实质影响力，超过其他民间社团，是最爱国的社团。<br />
<br />
　　刘会进诉说“洪门”的历史，在明朝末年，明朝名将郑成功派将领陈永华（化名陈近南）到大陆湖北襄阳联合反清志士成立“洪门”，但清朝当时国力强盛，洪门反清复明失败，洪门昆仲失散各地，但以中华文化忠义精神继续凝聚“洪门”力量，化为“青帮”、“三和会”、“四川袍哥”、“哥老会”、“小刀会”、“天地会”等名称，继续在中国大陆活动。<br />
<br />
　　他说，当初孙中山就是在美国夏威夷参加洪门组织“致公堂”担任领袖，孙中山成立的“兴中会”、“同盟会”也是洪门组织，推翻清朝的黄花岗72烈士，有 69人是“洪门”子弟，可见洪门对中华民族的贡献。<br />
<br />
