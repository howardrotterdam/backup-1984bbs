中国犹太后裔在以色列<br />
<br />
<br />
<br />
四年半以前带著妻子、女儿辗转到达以色列后的第一个晚上，他彻夜难眠。第二天清晨他一个人步行几个小时到了耶路撒冷老城古犹太教堂遗址的哭墙，泪如泉涌。从那时起，在中国开封出生、长大的犹太后裔金广元先生和他的家人开始了在这个犹太国家的新生活。<br />
<br />
*辗转来到以色列<br />
<br />
金广元先生的希伯来名字叫Shlomo（即圣经中的索罗门王名字的希伯来读法），今年48岁，曾在国内经营锅炉配件厂。妻子张金玲不是犹太后裔。他们的女儿文静刚刚高中毕业。<br />
<br />
金先生讲述了他和家人来以色列的曲折过程。<br />
<br />
祖父生前经常跟孩子们说：「你们是犹太人，要回到圣地去。」父亲临终前对金家三兄弟说：「有机会一定要回到圣地。」父母过世后，回归圣地的愿望也越发强烈。<br />
<br />
从那以后金先后去北京四次。<br />
<br />
95年第一次去时是到北京参加展览会，一行三人顺路到以色列使馆，问是否可以回以色列。当时是使馆的中国工作人员接待。他们询问以色列对开封犹太后裔是甚么态度，并递交了资料，可是拒绝接受。原因是不承认他们是犹太人。<br />
<br />
一位名叫张广照的犹太后裔曾在95年以农业考察为由到过以色列。他当时就有移居以色列的愿望，就到以内务部询问开封的犹太人怎么办？以官员问：有甚么证据吗？开封有拉比（犹太教律法学家）吗？张回答说：没有。以官员问：政府承认吗？张出示户口簿，上写：「犹太人」。内务部官员说要公证书。张马上回国办理公证。<br />
<br />
第四次去北京之前，三位犹太后裔在开封当地的公证处做了犹太人身份的公证。当时的户口簿民族一栏填的就是「犹太」字样。可是公证一周后，中央下达文件到开封公证处，声明公证作废。如果公证不交出来将负法律责任。一个月以后，开封犹太人家庭的户口簿也被强制改成「汉族」或「回族」。<br />
<br />
张广照在北京把公证交给一位美国人，托他交给以色列使馆再转交以色列内务部确认。张乘火车回开封的途中，警察已经在他家等他。警察问：「都声明作废了，为甚么还交给以色列使馆？」说是以色列使馆交给中国政府了。张被逼迫写悔过书。警察逼问公证书的去处，张回答说交给芬兰人了。原来公证过后三天交给了来到开封的芬兰犹太人。<br />
<br />
99年，金先生一家办理去芬兰旅游。芬兰的犹太组织帮助办理去芬兰的签证，两周后办成。当时警察警告说：不许移民。当时只有夫妇俩和女儿知道要出国。金跟外人说是去广州工作。临行一周前，金把工厂卖掉、分掉。家里的房子卖掉后搬了出来，对外人说是要装修。连邻居和弟弟都不知道是要出国，都说装修好了再搬回去。直到飞机起飞前，金才给家里打了电话道别。<br />
<br />
金夫妇俩人带著女儿到芬兰后，女儿第二天就开始在当地的学校上学,并向当地的犹太人学习希伯来语。在芬兰居留期间等待以色列入境批准。刚好4个月后芬兰签证到期时，以色列的旅游签证批准下来了。就这样，一家三口来到了以色列。<br />
<br />
*开封的犹太人<br />
<br />
史料表明，早在唐代就有犹太人随商队从陆路经古丝绸之路来到中国。到了北宋，在繁华的都市开封形成了一个规模较大的犹太社团。<br />
<br />
1856年一位加拿大人来到开封，看到了教堂、拉比，还画了图。<br />
<br />
在金先生的记忆中，文革前的开封犹太后裔保留著犹太人的风俗。据父亲讲，过犹太节日「逾越节」时家里吃不发面饼和淹一年的酸菜。除此之外还过犹太新年，但由于与其他犹太社区的隔离，唯一的依据就是圣经，所以日期可能和以色列的历法不太一致。<br />
<br />
文革期间，金先生的父亲受到了冲击，被打成「以色列特务」。当时曾经挂牌游街。很多犹太传统的书籍也被烧毁。<br />
<br />
70年代文革后，美国、加拿大、澳大利亚等国的犹太人开始到开封寻找犹太人。当时和犹太后裔见面要经过中国旅游局批准。见面前当地的犹太家庭要经过「培训」，教他们在外国人面前如何说。「国家对待犹太人好不好？」──「好！」「受歧视吗？」──「不受。」<br />
<br />
如今在开封有400多户犹太后裔家庭，大约2000多人。每个家庭都知道谁家是犹太人，谁家不是。<br />
<br />
金先生说，犹太后裔家庭保持Kosher，即犹太律法中的饮食戒律。金先生在家时每周过「沙巴特」，即犹太人的周六安息日，仪式上和以色列一样。周五太阳下山前读圣经，一个人读，其他人听，还有唱赞美歌。可是人一多，警察就来干涉。曾有一次16位亲友一起过「沙巴特」，公安知道后通知说以后不许非法集会。从那以后只好每家自己过「沙巴特」。公安一直监视，不间断。<br />
<br />
金先生曾找过开封市市长，要求恢复犹太教的信仰自由。当时的副市长张家顺说要注册，但又不准注册。如果要强行聚会，就要受法律制裁。<br />
<br />
问到为甚么犹太人被压制，金先生说，犹太人向来多灾多难。不管别人怎么看，都要在上帝安排的路上走下去，因为是上帝的选民。他讲述了一段历史：<br />
<br />
1952年，北京召开了民族代表大会。开封犹太人后裔去了两个人。在天安门城楼上，毛泽东主席问：「你们是甚么民族？」两人答道：「我们是犹太人。」过后毛出一文：承认56个民族，不承认犹太人。说是历史遗留问题，可管可不管。<br />
<br />
*是否犹太人？<br />
<br />
金先生到达以色列后第二天步行几个小时到了耶路撒冷老城古犹太教堂遗址的哭墙，泪如泉涌──因为家族世代的愿望实现了。他赞美上帝，并向上帝请求，让开封所有的犹太后裔都回到以色列。当天在哭墙六个小时，周围有拉比曾试图跟他讲话，可是语言不通。<br />
<br />
如今金先生一家三口住在耶路撒冷装饰简朴的公寓里。一进门就看到犹太人节日的烛台和盘子。客厅里挂著几幅旧时开封犹太人教堂及其周围景物的画像。<br />
<br />
在以色列四年多了，仍然没有合法工作和居留身份，因此夫妇俩人没有工作。中国护照也过期了。生活难一些，但金先生说心中有上帝，就够了。<br />
<br />
刚到以色列时曾和妻子女儿一起到速成班学习希伯来语。如今他每天去教堂读经。平时有时去买菜，晚上去散步，周五「沙巴特」之前去踢球。生活的支出由芬兰的基金会资助。这个基金会同时帮助前苏联国家的犹太人回归以色列。<br />
<br />
2001年经由人介绍去耶路撒冷的一所教堂，介绍给当地的拉比。教堂的人很友善，金先生和妻子每个「沙巴特」去一位拉比家庭共进午餐。<br />
<br />
由于最近以色列移民警察对非法外籍劳工抓得很紧，金先生还曾经被逮捕。监狱里同囚的一位以色列人看到金先生戴著犹太人的小帽说：「你是假的！你是怕被抓才戴犹太帽的。」并且把他挤到一边。晚上祈祷时，金先生用希伯来语祈祷。那个以色列人说：「原来你真是犹太人。」索性把自己睡觉的床让给了金先生。<br />
<br />
以色列内务部不承认金先生一家是犹太人。原因是按照以色列的回归法，犹太人身份是由母系传的。而开封的犹太后裔是父系传的。因此以色列官方不承认开封的犹太人。金先生本人目前仍未按犹太教规行割礼。妻子希伯来名叫Dina，虽然不是犹太后裔，但是觉得自己和犹太人一样，并支持、参与丈夫的信仰。<br />
<br />
问到有何打算，金先生说，如果以色列不接受他，他可以回开封，组织大家学习圣经。女儿在靠近以色列北部城市海法的宗教学校刚刚毕业，准备皈依犹太教，可能对父母的移民有些影响。皈依过程包括学习犹太律法。他本人和妻子也许会同意皈依犹太教。但他以前向来认为：「我是犹太人，为甚么还要『皈依』犹太教呢？」<br />
<br />
*最大愿望-让更多人帮助开封犹太人回归以色列<br />
<br />
金先生的最大愿望就是让更多的人知道开封犹太人，让更多的人来帮助他们回到以色列。<br />
<br />
当问到是否爱国，金先生说：「生我养我的是父母。中国收留了我，我很感激。要代表父母感激中国的宽容，没有让犹太人在中国灭亡。」<br />
<br />
金先生说：「宗教信仰应该是自由的。不让人信仰，就没有信仰自由。信仰是精神的支柱，人都应该有一个信仰。信仰会让人向更好的方向发展。」<br />
<br />
*女儿谈以色列生活<br />
<br />
金先生的女儿文静，希伯来名叫Shalva，意思是「平静」。她描述了在以色列的生活。<br />
<br />
「四年半以前我跟父母来到以色列。当时我和父母到速成班学习希伯来语。速成班的所有课程都学完了，我们就开始找学校。我们在耶路撒冷找到几家学校，由于我的年龄和语言的关系，他们没有收我。<br />
<br />
「经过朋友的帮助，我到一个为新移民青年设立的宗教村庄。这里多数学生都来自埃塞俄比亚和俄罗斯。人们对我很好。所有的人对我都很好奇，总是问我问题。很快的，大家就都认识我了，可是直到现在我仍然不知道所有人的名字。<br />
<br />
「我选择在这个地方学习还因为我要皈依犹太教。这个地方不仅仅是一所学校，而且帮助学生完成皈依过程。因此我从10年级起就加入了皈依程序。由于在中国我从来没听说过『皈依』是甚么，我感觉甚么都怪怪的。因为这是个宗教村庄，每天早晨要祈祷。女生每天一次就够了，男生则要每天祈祷三次。除了个别细节之外，男女生的祈祷内容几乎一样，可是遵从的戒律不一样。女生皈依要学的比男生要少。虽然如此，开始的时候对我来说仍然很困难。<br />
<br />
「我在家里是独生女，从来没有过出门远离父母的经验。（父母住在耶路撒冷，我学习的地方要住校，靠近海法。）也由于自己是女孩，加上年龄和语言的关系，我觉得很难与人沟通。有一次正上课时因为想念父母，我哭了起来。就这样，在第一年里我学了有关犹太教的基础知识，而且对犹太教有许多疑惑。但是随著时间，慢慢的我习惯了村庄里的生活，学习与人沟通，并试著走入犹太教。<br />
<br />
「对于拉比们来讲，要皈依犹太教就要表示自己的诚意。不仅仅要学习，还要付诸于行动，例如每天祈祷，每周五、六上教堂。不但要有行动，还要让大家都看到你想成为犹太人的诚意，所以要很长时间来观察。然后要看你对犹太教懂的够不够，根据这些来决定甚么时候正式皈依。因为我对犹太教的疑惑和提问，我的整个过程共用了三年的时间。通常学习皈依课程后一年就可以完成皈依过程，可是这也看每个人的意志，因人而异。<br />
<br />
「其实虽然我在以色列只有四年半，我感觉以色列好像我的家一样。我已经开始忘记我的中文。可是因为是在中国长大的，我仍然对中国有感情。我想念中国，为中国的发展而高兴。我很想去看看中国。在我内心我仍感觉我的一部份是属于中国的。因此我想在大学里继续学习中文，并学习经济学或企业管理。我希望在与国以有关的领域工作。<br />
<br />
「我觉得以色列的生活比在中国自由。在以色列孩子们从小就会独立生活，14到16岁就开始在商店、餐馆、及其他各种地方工作。他们知道想要有钱花就要去打工挣钱。大家都知道如果想要甚么，要努力才能得到应得的东西。以色列人不会放弃他们应得的东西。<br />
<br />
「在以色列我很难习惯的就是人们的『乎次巴』[注：可译为「冒昧」、「放肆」、「傲慢」、「鲁莽」。]。他们总是有甚么事当面就说，不管是关于你的好事还是坏事，他们就直接了当的跟你说对你的看法，并问他们感兴趣的问题。这是以色列人的文化，其实是很好的事，可是对于生活在其他文化中的人来说这是很难接受的。」<br />
