From Kaifeng to kibbutzim<br />
Oct. 22, 2009<br />
Jerusalem Post staff , THE JERUSALEM POST<br />
Ben-Gurion Airport was the setting for an unusual scene late Tuesday<br />
night, as seven young Chinese men wearing kippot arrived via<br />
Uzbekistan to make aliya.<br />
<br />
The newcomers, who were brought here by the Shavei Israel<br />
organization, are all descendants of the Jewish community of Kaifeng,<br />
China, which flourished on the southern banks of the Yellow River for<br />
more than 1,000 years.<br />
<br />
It marked the first time that an organized group has moved here from Kaifeng.<br />
<br />
&quot;I am very excited to be here in the Holy Land,&quot; said Yaakov Wang, one<br />
of the new immigrants. &quot;This is something that my ancestors dreamed<br />
about for generations, and now, thank God, I have finally made it.&quot;<br />
<br />
Wang said he eventually hoped to become a rabbi, so that one day he<br />
could help other Kaifeng Jewish descendants to learn more about their<br />
heritage.<br />
<br />
Wang and the other young men will spend their first few months in the<br />
country learning Hebrew at an ulpan on a religious kibbutz in the<br />
North, after which they will prepare to undergo conversion.<br />
<br />
From the airport, the group went straight to the Western Wall, where<br />
they recited the &quot;Shehehiyanu&quot; blessing with great emotion, and then<br />
burst into a chorus of traditional Hebrew songs.<br />
<br />
&quot;It took more than two years to get the necessary permission from the<br />
Interior Ministry to bring them over, but it was well worth the wait,&quot;<br />
said Shavei Israel chairman Michael Freund, who is also a Jerusalem<br />
Post columnist. Freund said he hoped this group's arrival and<br />
absorption would serve as a pilot, and if successful, would open the<br />
door to bringing more potential olim.<br />
<br />
&quot;This is an historic event,&quot; he said, adding that &quot;Kaifeng's Jewish<br />
descendants are a living link between China and the Jewish people, and<br />
it is very moving to see the remnants of this community returning to<br />
their roots.&quot;<br />
<br />
At its peak, during the Middle Ages, Kaifeng Jewry numbered about<br />
5,000. But widespread intermarriage and assimilation, as well as the<br />
death of the community's last rabbi, brought about its demise by the<br />
middle of the 19th century.<br />
<br />
According to Freund, there are still hundreds of people in Kaifeng who<br />
cling to their identity as descendants of the city's Jewish community.<br />
In recent years, a growing number have begun to express an interest in<br />
studying Jewish history and culture. The building where the synagogue<br />
stood is now a hospital. In the basement, there are still signs of a<br />
mikve.<br />
<br />
Of the approximately 1,000 descendants of the Jewish community, about<br />
half, if given the opportunity, would make the move to Israel, Freund<br />
said.<br />
