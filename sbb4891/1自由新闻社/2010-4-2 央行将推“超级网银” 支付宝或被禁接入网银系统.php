													<h2>央行将推“超级网银” 支付宝或被禁接入网银系统</h2>
						
						
																											<div id="postmessage_545222" class="t_msgfont">2010-04-02 09:54:36　来源: 人民网(北京)　跟贴 945 条 手机看新闻 <br />
<br />
核心提示：据悉，网银互联应用系统或将于今年8月份上线运行。该网银互联应用系统可谓“超级网银”，能提供跨行实时的资金汇划、跨行账户和账务查询，以及跨行扣款、第三方支付、第三方预授权等业务功能。知情人士透露，支付宝等第三方支付平台或被暂停接入网银互联系统。<br />
<br />
人民网4月2日报道&nbsp;&nbsp;中国银行业的网银分隔壁垒将很快被打破。<br />
<br />
4月1日，记者从商业银行渠道获悉，央行着力构建的第二代支付系统的“先行军”――网银互联应用系统或将于今年8月份上线运行，从此消解国内银行业金融机构网银的无序竞争状态。目前，首批十多家试点的主要商业银行正紧张进行接入系统开发。<br />
<br />
知情人士透露，该系统将作为现有支付系统的有效补充，与大额实时支付系统、小额批量支付系统并行运营，业务金额上限介于两者之间，初定为5万元。<br />
<br />
此外，一度引起各银行担忧的第三方支付平台或被暂停接入网银互联系统。“如果第三方平台接入，将对商业银行支付业务产生重大冲击。”一股份制银行人士说。<br />
<br />
央行的“超级网银”<br />
<br />
互联网的勃兴和新兴电子支付的发展，令央行第一代人民币跨行支付系统改造升级迫在眉睫。<br />
<br />
传统的支付系统已无法满足需求的不断增长。一般的支付业务，当付款人需要向他行账户付款时，该笔款项将从付款行网上银行系统，经过付款人开户银行行内业务系统、人民银行现代化支付系统、收款人开户银行行内业务系统等多个环节。支付业务指令须经过多个节点、多个系统间的转换，效率低、成本高。<br />
<br />
为满足银行业金融机构间的支付业务实时清算需要，2009年12月2日，央行召开第二代支付系统暨中央银行会计核算数据集中系统(简称ACS系统)建设启动会议，阐明第二代支付系统能为银行业金融机构提供灵活的接入方式、清算模式和更加全面的流动性风险管理手段。<br />
<br />
4月1日，知情人士称，按照原计划，第二代支付系统的网银互联应用系统先行建设，原本拟于2010年6月底前上线运行，第二代支付系统其他应用系统和ACS系统于2011年6月底前上线运行。不过从目前准备情形看，网银互联系统可能要到8月份上线。<br />
<br />
该网银互联应用系统可谓“超级网银”，能为个人和单位用户提供跨行实时的资金汇划、跨行账户和账务查询，以及当下支付系统所无法实现的跨行扣款、第三方支付、第三方预授权等业务功能，为商业银行在电子商务、跨行资金管理等方面为客户提供创新服务奠定有力基础。<br />
<br />
“零售业务必须上线，对公业务可上可不上。”上述知情人士透露，央行可能考虑到安全性和效率，没有强制对公业务上线。<br />
<br />
“这一系统在运行上与大额实时支付系统、小额批量支付系统形成补充，各有侧重。”知情人士说，小额支付系统贷记业务金额上限为2万元，网银互联系统的金额上限初定为5万元，5万元以上的支付业务要使用大额支付系统。<br />
<br />
“此前，金额在2万以下的用大额或小额都可以，但做小额支付银行的手续费支出比大额的要少很多。网银互联实现后将节省银行成本。”该人士说。<br />
<br />
银行或免受第三方支付平台威胁<br />
<br />
网银联通之后，将打破支付清算组织间的壁垒，对商业银行带来深远影响。<br />
<br />
部分银行担忧，网银互联后，客户可能被别的银行抢走，存款被挖走，尤其是当前各银行普遍面临资本金不足。“这将考验各银行的服务能力，也将推动网银服务更上台阶。”上述股份行人士认为。<br />
<br />
国内网银近年来虽取得不错成绩，但整体而言，网银普及率并不高。中国金融认证中心年初发布的《2009中国网上银行调查报告》显示，2009年，个人网银用户比例为20.9%，比2008年增长2%;企业网银用户当年比例为40.5%，比2008年略有下降。为提高本行的网银普及率，一些银行在网银业务收费上打价格战屡见不鲜。<br />
<br />
“网银互联系统的推出，将有利于各银行的有序竞争。”上述人士评述。<br />
<br />
规范银行业内秩序只被网银从业者看作新系统带来的一般影响，最令他们担忧的是第三方支付平台的冲击。<br />
<br />
此前，银行间有一种猜想，各银行和“支付宝”等第三方支付平台可能都将作为主体接入央行网银互联系统，第三方平台因业务量日益增大，势必对银行支付业务造成较大冲击。<br />
<br />
不过，银行或许能暂时免受第三方平台威胁。银行业人士透露，央行给银行的反馈是，或许暂停第三方支付平台接入网银互联系统。“主要原因在于，第三方平台仍未获得相关牌照，不属结算清算组织。”前述人士透露。<br />
<br />
不过，多位银行人士亦承认，第三方支付的发展壮大是必然趋势，后期仍可能会接入网银互联系统，这将促使银行改进和完善服务。<br />
<br />
 <br />
<br />
易观国际2009年12月发布的《中国第三方支付市场蓝皮书》显示，中国第三方支付市场经过10年的发展，年度交易规模已经接近6000亿，成为中国金融支付体系中重要的组成部分。按照这种井喷趋势，两年内其交易规模有望突破万亿。<br />
<br />
“银行与第三方支付是既合作又抵触。”银行业人士坦言，网上购物可以提升持卡人的用卡积极性，对银行业务长远发展有积极作用。但矛盾的另一面是，持卡人通过第三方支付平台交易，银行失去了商户回佣，得不到任何收益。如何解决合作中的话语权与利益分配问题，是解决双方分歧的焦点。<br />
