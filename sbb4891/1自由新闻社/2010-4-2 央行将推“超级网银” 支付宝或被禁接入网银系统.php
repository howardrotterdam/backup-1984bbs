													<h2>央行规定第三方不经批准不得从事网络支付  支付宝或被禁入</h2>
						
						
																											<div id="postmessage_695949" class="t_msgfont"><strong>央行规定非金融机构不经批准不得从事网络支付</strong><br />
<br />
2010-06-21 15:44:00　来源: 中国新闻网(北京)<br />
<br />
核心提示：中国人民银行制定并公布了《非金融机构支付服务管理办法》，该办法自2010年9月1日起施行。央行规定非金融机构不经批准不得从事网络支付。<br />
中新网6月21日电 据央行网站消息，中国人民银行制定并公布了《非金融机构支付服务管理办法》，该办法自2010年9月1日起施行。<br />
<br />
全文如下:<br />
<br />
<strong>中国人民银行令〔2010〕第2号</strong><br />
<br />
根据《中华人民共和国中国人民银行法》等法律法规，中国人民银行制定了《非金融机构支付服务管理办法》，经2010年5月19日第7次行长办公会议通过，现予公布，自2010年9月1日起施行。<br />
<br />
行长：周小川<br />
<br />
二一年六月十四日<br />
<br />
<strong>非金融机构支付服务管理办法</strong><br />
<br />
第一章 总 则<br />
<br />
第一条 为促进支付服务市场健康发展，规范非金融机构支付服务行为，防范支付风险，保护当事人的合法权益，根据《中华人民共和国中国人民银行法》等法律法规，制定本办法。<br />
<br />
第二条 本办法所称非金融机构支付服务，是指非金融机构在收付款人之间作为中介机构提供下列部分或全部货币资金转移服务：<br />
<br />
(一)网络支付；<br />
<br />
(二)预付卡的发行与受理；<br />
<br />
(三)银行卡收单；<br />
<br />
(四)中国人民银行确定的其他支付服务。<br />
<br />
本办法所称网络支付，是指依托公共网络或专用网络在收付款人之间转移货币资金的行为，包括货币汇兑、互联网支付、移动电话支付、固定电话支付、数字电视支付等。<br />
<br />
本办法所称预付卡，是指以营利为目的发行的、在发行机构之外购买商品或服务的预付价值，包括采取磁条、芯片等技术以卡片、密码等形式发行的预付卡。<br />
<br />
本办法所称银行卡收单，是指通过销售点(POS)终端等为银行卡特约商户代收货币资金的行为。<br />
<br />
第三条 非金融机构提供支付服务，应当依据本办法规定取得《支付业务许可证》，成为支付机构。<br />
<br />
支付机构依法接受中国人民银行的监督管理。<br />
<br />
未经中国人民银行批准，任何非金融机构和个人不得从事或变相从事支付业务。<br />
<br />
第四条 支付机构之间的货币资金转移应当委托银行业金融机构办理，不得通过支付机构相互存放货币资金或委托其他支付机构等形式办理。<br />
<br />
支付机构不得办理银行业金融机构之间的货币资金转移，经特别许可的除外。<br />
<br />
第五条 支付机构应当遵循安全、效率、诚信和公平竞争的原则，不得损害国家利益、社会公共利益和客户合法权益。<br />
<br />
第六条 支付机构应当遵守反洗钱的有关规定，履行反洗钱义务。<br />
<br />
第二章 申请与许可<br />
<br />
第七条 中国人民银行负责《支付业务许可证》的颁发和管理。<br />
<br />
申请《支付业务许可证》的，需经所在地中国人民银行分支机构审查后，报中国人民银行批准。<br />
<br />
本办法所称中国人民银行分支机构，是指中国人民银行副省级城市中心支行以上的分支机构。<br />
<br />
第八条 《支付业务许可证》的申请人应当具备下列条件:<br />
<br />
(一)在中华人民共和国境内依法设立的有限责任公司或股份有限公司，且为非金融机构法人；<br />
<br />
(二)有符合本办法规定的注册资本最低限额；<br />
<br />
(三)有符合本办法规定的出资人；<br />
<br />
(四)有5名以上熟悉支付业务的高级管理人员；<br />
<br />
(五)有符合要求的反洗钱措施；<br />
<br />
(六)有符合要求的支付业务设施；<br />
<br />
(七)有健全的组织机构、内部控制制度和风险管理措施；<br />
<br />
(八)有符合要求的营业场所和安全保障措施；<br />
<br />
(九)申请人及其高级管理人员最近3年内未因利用支付业务实施违法犯罪活动或为违法犯罪活动办理支付业务等受过处罚。<br />
<br />
第九条 申请人拟在全国范围内从事支付业务的，其注册资本最低限额为1亿元人民币；拟在省(自治区、直辖市)范围内从事支付业务的，其注册资本最低限额为3千万元人民币。注册资本最低限额为实缴货币资本。<br />
<br />
本办法所称在全国范围内从事支付业务，包括申请人跨省(自治区、直辖市)设立分支机构从事支付业务，或客户可跨省(自治区、直辖市)办理支付业务的情形。<br />
<br />
中国人民银行根据国家有关法律法规和政策规定，调整申请人的注册资本最低限额。<br />
<br />
外商投资支付机构的业务范围、境外出资人的资格条件和出资比例等，由中国人民银行另行规定，报国务院批准。<br />
<br />
第十条 申请人的主要出资人应当符合以下条件：<br />
<br />
(一)为依法设立的有限责任公司或股份有限公司；<br />
<br />
(二)截至申请日，连续为金融机构提供信息处理支持服务2年以上，或连续为电子商务活动提供信息处理支持服务2年以上；<br />
<br />
(三)截至申请日，连续盈利2年以上；<br />
<br />
(四)最近3年内未因利用支付业务实施违法犯罪活动或为违法犯罪活动办理支付业务等受过处罚。<br />
<br />
本办法所称主要出资人，包括拥有申请人实际控制权的出资人和持有申请人10%以上股权的出资人。<br />
<br />
第十一条 申请人应当向所在地中国人民银行分支机构提交下列文件、资料:<br />
<br />
(一)书面申请，载明申请人的名称、住所、注册资本、组织机构设置、拟申请支付业务等；<br />
<br />
(二)公司营业执照(副本)复印件；<br />
<br />
(三)公司章程；<br />
<br />
(四)验资证明；<br />
<br />
(五)经会计师事务所审计的财务会计报告；<br />
<br />
(六)支付业务可行性研究报告；<br />
<br />
(七)反洗钱措施验收材料；<br />
<br />
(八)技术安全检测认证证明；<br />
<br />
(九)高级管理人员的履历材料；<br />
<br />
(十)申请人及其高级管理人员的无犯罪记录证明材料；<br />
<br />
(十一)主要出资人的相关材料；<br />
<br />
(十二)申请资料真实性声明。<br />
<br />
第十二条 申请人应当在收到受理通知后按规定公告下列事项：<br />
<br />
(一)申请人的注册资本及股权结构；<br />
<br />
(二)主要出资人的名单、持股比例及其财务状况；<br />
<br />
(三)拟申请的支付业务；<br />
<br />
(四)申请人的营业场所；<br />
<br />
(五)支付业务设施的技术安全检测认证证明。<br />
<br />
第十三条 中国人民银行分支机构依法受理符合要求的各项申请，并将初审意见和申请资料报送中国人民银行。中国人民银行审查批准的，依法颁发《支付业务许可证》，并予以公告。<br />
<br />
《支付业务许可证》自颁发之日起，有效期5年。支付机构拟于《支付业务许可证》期满后继续从事支付业务的，应当在期满前6个月内向所在地中国人民银行分支机构提出续展申请。中国人民银行准予续展的，每次续展的有效期为5年。<br />
<br />
第十四条 支付机构变更下列事项之一的，应当在向公司登记机关申请变更登记前报中国人民银行同意：<br />
<br />
(一)变更公司名称、注册资本或组织形式；<br />
<br />
(二)变更主要出资人；<br />
<br />
(三)合并或分立；<br />
<br />
(四)调整业务类型或改变业务覆盖范围。<br />
<br />
第十五条 支付机构申请终止支付业务的，应当向所在地中国人民银行分支机构提交下列文件、资料：<br />
<br />
(一)公司法定代表人签署的书面申请，载明公司名称、支付业务开展情况、拟终止支付业务及终止原因等；<br />
<br />
(二)公司营业执照(副本)复印件；<br />
<br />
(三)《支付业务许可证》复印件；<br />
<br />
(四)客户合法权益保障方案；<br />
<br />
(五)支付业务信息处理方案。<br />
<br />
准予终止的，支付机构应当按照中国人民银行的批复完成终止工作，交回《支付业务许可证》。<br />
<br />
第十六条 本章对许可程序未作规定的事项，适用《中国人民银行行政许可实施办法》(中国人民银行令〔2004〕第3号)。<br />
<br />
第三章 监督与管理<br />
<br />
第十七条 支付机构应当按照《支付业务许可证》核准的业务范围从事经营活动，不得从事核准范围之外的业务，不得将业务外包。<br />
<br />
支付机构不得转让、出租、出借《支付业务许可证》。<br />
<br />
第十八条 支付机构应当按照审慎经营的要求，制订支付业务办法及客户权益保障措施，建立健全风险管理和内部控制制度，并报所在地中国人民银行分支机构备案。<br />
<br />
第十九条 支付机构应当确定支付业务的收费项目和收费标准，并报所在地中国人民银行分支机构备案。<br />
<br />
支付机构应当公开披露其支付业务的收费项目和收费标准。<br />
<br />
第二十条 支付机构应当按规定向所在地中国人民银行分支机构报送支付业务统计报表和财务会计报告等资料。<br />
<br />
第二十一条 支付机构应当制定支付服务协议，明确其与客户的权利和义务、纠纷处理原则、违约责任等事项。<br />
<br />
支付机构应当公开披露支付服务协议的格式条款，并报所在地中国人民银行分支机构备案。<br />
<br />
第二十二条 支付机构的分公司从事支付业务的，支付机构及其分公司应当分别到所在地中国人民银行分支机构备案。<br />
<br />
支付机构的分公司终止支付业务的，比照前款办理。<br />
<br />
第二十三条 支付机构接受客户备付金时，只能按收取的支付服务费向客户开具发票，不得按接受的客户备付金金额开具发票。<br />
<br />
第二十四条 支付机构接受的客户备付金不属于支付机构的自有财产。<br />
<br />
支付机构只能根据客户发起的支付指令转移备付金。禁止支付机构以任何形式挪用客户备付金。<br />
<br />
第二十五条 支付机构应当在客户发起的支付指令中记载下列事项：<br />
<br />
(一)付款人名称；<br />
<br />
(二)确定的金额；<br />
<br />
(三)收款人名称；<br />
<br />
(四)付款人的开户银行名称或支付机构名称；<br />
<br />
(五)收款人的开户银行名称或支付机构名称；<br />
<br />
(六)支付指令的发起日期。<br />
<br />
客户通过银行结算账户进行支付的，支付机构还应当记载相应的银行结算账号。客户通过非银行结算账户进行支付的，支付机构还应当记载客户有效身份证件上的名称和号码。<br />
<br />
第二十六条 支付机构接受客户备付金的，应当在商业银行开立备付金专用存款账户存放备付金。中国人民银行另有规定的除外。<br />
<br />
支付机构只能选择一家商业银行作为备付金存管银行，且在该商业银行的一个分支机构只能开立一个备付金专用存款账户。<br />
<br />
支付机构应当与商业银行的法人机构或授权的分支机构签订备付金存管协议，明确双方的权利、义务和责任。<br />
<br />
支付机构应当向所在地中国人民银行分支机构报送备付金存管协议和备付金专用存款账户的信息资料。<br />
<br />
第二十七条 支付机构的分公司不得以自己的名义开立备付金专用存款账户，只能将接受的备付金存放在支付机构开立的备付金专用存款账户。<br />
<br />
第二十八条 支付机构调整不同备付金专用存款账户头寸的，由备付金存管银行的法人机构对支付机构拟调整的备付金专用存款账户的余额情况进行复核，并将复核意见告知支付机构及有关备付金存管银行。<br />
<br />
支付机构应当持备付金存管银行的法人机构出具的复核意见办理有关备付金专用存款账户的头寸调拨。<br />
<br />
第二十九条 备付金存管银行应当对存放在本机构的客户备付金的使用情况进行监督，并按规定向备付金存管银行所在地中国人民银行分支机构及备付金存管银行的法人机构报送客户备付金的存管或使用情况等信息资料。<br />
<br />
对支付机构违反第二十五条至第二十八条相关规定使用客户备付金的申请或指令，备付金存管银行应当予以拒绝；发现客户备付金被违法使用或有其他异常情况的，应当立即向备付金存管银行所在地中国人民银行分支机构及备付金存管银行的法人机构报告。<br />
<br />
第三十条 支付机构的实缴货币资本与客户备付金日均余额的比例，不得低于10%。<br />
<br />
本办法所称客户备付金日均余额，是指备付金存管银行的法人机构根据最近90日内支付机构每日日终的客户备付金总量计算的平均值。<br />
<br />
第三十一条 支付机构应当按规定核对客户的有效身份证件或其他有效身份证明文件，并登记客户身份基本信息。<br />
<br />
支付机构明知或应知客户利用其支付业务实施违法犯罪活动的，应当停止为其办理支付业务。<br />
<br />
第三十二条 支付机构应当具备必要的技术手段，确保支付指令的完整性、一致性和不可抵赖性，支付业务处理的及时性、准确性和支付业务的安全性；具备灾难恢复处理能力和应急处理能力，确保支付业务的连续性。<br />
<br />
第三十三条 支付机构应当依法保守客户的商业秘密，不得对外泄露。法律法规另有规定的除外。<br />
<br />
第三十四条 支付机构应当按规定妥善保管客户身份基本信息、支付业务信息、会计档案等资料。<br />
<br />
第三十五条 支付机构应当接受中国人民银行及其分支机构定期或不定期的现场检查和非现场检查，如实提供有关资料，不得拒绝、阻挠、逃避检查，不得谎报、隐匿、销毁相关证据材料。<br />
<br />
第三十六条 中国人民银行及其分支机构依据法律、行政法规、中国人民银行的有关规定对支付机构的公司治理、业务活动、内部控制、风险状况、反洗钱工作等进行定期或不定期现场检查和非现场检查。<br />
<br />
中国人民银行及其分支机构依法对支付机构进行现场检查，适用《中国人民银行执法检查程序规定》(中国人民银行令〔2010〕第1号发布)。<br />
<br />
第三十七条 中国人民银行及其分支机构可以采取下列措施对支付机构进行现场检查：<br />
<br />
(一)询问支付机构的工作人员，要求其对被检查事项作出解释、说明；<br />
<br />
(二)查阅、复制与被检查事项有关的文件、资料，对可能被转移、藏匿或毁损的文件、资料予以封存；<br />
<br />
(三)检查支付机构的客户备付金专用存款账户及相关账户；<br />
<br />
(四)检查支付业务设施及相关设施。<br />
<br />
第三十八条 支付机构有下列情形之一的，中国人民银行及其分支机构有权责令其停止办理部分或全部支付业务：<br />
<br />
(一)累计亏损超过其实缴货币资本的50%；<br />
<br />
(二)有重大经营风险；<br />
<br />
(三)有重大违法违规行为。<br />
<br />
第三十九条 支付机构因解散、依法被撤销或被宣告破产而终止的，其清算事宜按照国家有关法律规定办理。<br />
<br />
第四章 罚 则<br />
<br />
第四十条 中国人民银行及其分支机构的工作人员有下列情形之一的，依法给予行政处分；构成犯罪的，依法追究刑事责任：<br />
<br />
(一)违反规定审查批准《支付业务许可证》的申请、变更、终止等事项的；<br />
<br />
(二)违反规定对支付机构进行检查的；<br />
<br />
(三)泄露知悉的国家秘密或商业秘密的；<br />
<br />
(四)滥用职权、玩忽职守的其他行为。<br />
<br />
第四十一条 商业银行有下列情形之一的，中国人民银行及其分支机构责令其限期改正，并给予警告或处1万元以上3万元以下罚款；情节严重的，中国人民银行责令其暂停或终止客户备付金存管业务：<br />
<br />
(一)未按规定报送客户备付金的存管或使用情况等信息资料的；<br />
<br />
(二)未按规定对支付机构调整备付金专用存款账户头寸的行为进行复核的；<br />
<br />
(三)未对支付机构违反规定使用客户备付金的申请或指令予以拒绝的。<br />
<br />
第四十二条 支付机构有下列情形之一的，中国人民银行分支机构责令其限期改正，并给予警告或处1万元以上3万元以下罚款：<br />
<br />
(一)未按规定建立有关制度办法或风险管理措施的；<br />
<br />
(二)未按规定办理相关备案手续的；<br />
<br />
(三)未按规定公开披露相关事项的；<br />
<br />
(四)未按规定报送或保管相关资料的；<br />
<br />
(五)未按规定办理相关变更事项的；<br />
<br />
(六)未按规定向客户开具发票的；<br />
<br />
(七)未按规定保守客户商业秘密的。<br />
<br />
第四十三条 支付机构有下列情形之一的，中国人民银行分支机构责令其限期改正，并处3万元罚款；情节严重的，中国人民银行注销其《支付业务许可证》；涉嫌犯罪的，依法移送公安机关立案侦查；构成犯罪的，依法追究刑事责任：<br />
<br />
(一)转让、出租、出借《支付业务许可证》的；<br />
<br />
(二)超出核准业务范围或将业务外包的；<br />
<br />
(三)未按规定存放或使用客户备付金的；<br />
<br />
(四)未遵守实缴货币资本与客户备付金比例管理规定的；<br />
<br />
(五)无正当理由中断或终止支付业务的；<br />
<br />
(六)拒绝或阻碍相关检查监督的；<br />
<br />
(七)其他危及支付机构稳健运行、损害客户合法权益或危害支付服务市场的违法违规行为。<br />
<br />
第四十四条 支付机构未按规定履行反洗钱义务的，中国人民银行及其分支机构依据国家有关反洗钱法律法规等进行处罚；情节严重的，中国人民银行注销其《支付业务许可证》。<br />
<br />
第四十五条 支付机构超出《支付业务许可证》有效期限继续从事支付业务的，中国人民银行及其分支机构责令其终止支付业务；涉嫌犯罪的，依法移送公安机关立案侦查；构成犯罪的，依法追究刑事责任。<br />
<br />
第四十六条 以欺骗等不正当手段申请《支付业务许可证》但未获批准的，申请人及持有其5%以上股权的出资人3年内不得再次申请或参与申请《支付业务许可证》。<br />
<br />
以欺骗等不正当手段申请《支付业务许可证》且已获批准的，由中国人民银行及其分支机构责令其终止支付业务，注销其《支付业务许可证》；涉嫌犯罪的，依法移送公安机关立案侦查；构成犯罪的，依法追究刑事责任；申请人及持有其5%以上股权的出资人不得再次申请或参与申请《支付业务许可证》。<br />
<br />
<br />
第四十七条 任何非金融机构和个人未经中国人民银行批准擅自从事或变相从事支付业务的，中国人民银行及其分支机构责令其终止支付业务；涉嫌犯罪的，依法移送公安机关立案侦查；构成犯罪的，依法追究刑事责任。<br />
<br />
第五章 附 则<br />
<br />
第四十八条 本办法实施前已经从事支付业务的非金融机构，应当在本办法实施之日起1年内申请取得《支付业务许可证》。逾期未取得的，不得继续从事支付业务。<br />
<br />
第四十九条 本办法由中国人民银行负责解释。<br />
<br />
第五十条 本办法自2010年9月1日起施行。<br />
