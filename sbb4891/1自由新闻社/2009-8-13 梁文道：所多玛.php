													<h2>梁文道：所多玛</h2>
						
						
																											<div id="postmessage_201471" class="t_msgfont">转自1510<br />
原文地址：<a href="http://www.my1510.cn/article.php?id=881f94586424f9e4" target="_blank">http://www.my1510.cn/article.php?id=881f94586424f9e4</a>（原文已经被和谐）<br />
<br />
欢迎来到所多玛。且让我为你介绍这座城市里所剩不多的善人。<br />
<br />
 <br />
<br />
例如许志永，香港中学生郑咏欣最近才在报刊上为他发表公开信，呼吁Wen Jiabao「用法理来说服我」，有情有理，令人慨叹，是一时焦点。在我看来，这封信最令人神伤的，是郑小姐记述许志永被捕几个月前还亲口对她解释别看截访的公安很野蛮，而要注意事情好转的那一面；他说：「中国政府已很努力，要对政府有多点耐性！」<br />
<br />
 <br />
<br />
你知道，每次在香港和台湾向别人介绍今天大陆的情况，都有人批评我的立场太过暧昧，取态太过温和。他们认为中国政府仍然是大海中那头凶猛的巨兽，独裁专制，噬人无算，而且绝无任何温和渐变的希望。而每一次，我都会告诉他们真实的情况很复杂，不要简单地总体化中国的问题，不要用刻版的偏见来看中国，而且「要对政府有点耐性」。<br />
<br />
 <br />
<br />
我的朋友许知远也写了一篇感人肺腑的《我们这一代》，他说许志永两年前曾经意气风发地对他表示「2008年的奥运会将给中国带来一次巨大变革机会。当全世界都盯着北京时，政治权力将有所收敛，而不同民间组织都刻利用良机，拓展公民社会的空间」。这番话我一点也不陌生，因为我也表达过类似的意见，我也曾对汶川地震和北京奥运之后的中国充满信心。每当外国记者找我谈论中国的黑暗角落，我都会在最后提醒他们，永远要看到光明的那一面，就如我曾提醒你一样。<br />
<br />
 <br />
<br />
而那光明的一面，就包括许志永和他公盟里的同伴，以及正在崛起的维权律师群体，与其它无数想做好事的热心人。这个国家腐败，这个社会冷漠，整个局面似乎就维系在一个十三亿人关于某则谎言的默契之上。尽管如此，却竟然还有这么多人愿意付出自己的时间，去替陌生人的子女奔走，如谭作人；竟还有这么多人愿意牺牲自己本来可以享受的生活，去替苦难无告的同胞叩门，如许志永。我甚至乐观到把政府也算进这光明的一面，因为至少他们曾经容许这种昏沉里的光芒摇曳。也许他们明白，连他们自己人都纷纷卷款而去，用脚对这里投下不信任票的时候，好人的存在有多么重要。天不丧予，如果你还能在所多玛找到一个好人的话。<br />
<br />
 <br />
<br />
他们把自己的子女送去外面，自由自在地上学成长；却让我们的孩子背负债务来接受可笑的「教育」。他们将自己的家人搬到北美和欧洲，享受干净的流水和清新的空气；却留给我们一片受伤并且中毒的土地。这个国家腐败如此，这个社会已然冷漠若斯。现在他们居然还要扼杀好人，并且恐吓其它人打消当好人的念头？没错。所以当你在公交车上被人打劫，高声求救，却发现满车没有一个人会伸出援手，甚至还别过头去的时候；不要讶异，因为我们鼓励这样的风气。<br />
<br />
 <br />
<br />
不过话又得说回来，当好人也不是不行，但前提是尽量不要自发。等到他们「动员」你了，你再调动自己的善念不迟，就像一个演员调动情绪来刺激泪腺一样。在这里，好人最好都是配合政府登场的演员，善意是种需要学习的演技；善恶的标准不来自头顶的星空，也不来自内心永恒的道德律，而在「感动中国」所界定的范围。你千万要小心，知道有人凌辱女子，可不能随便告发，因为你不知道那个强奸犯是谁；但如果听说一场运动会要召募志愿者了，那你得踊跃报名，不落人后。<br />
<br />
 <br />
<br />
忘记许志永吧，忘记那些你心目的「好人」，不要用你高高在上的标准来看待我们，对我们指手划脚。因为我们中国有自己的模式和道德尺度。<br />
<br />
 <br />
<br />
对了，听说过北京南站附近的「聚源宾馆」吗？里头监禁了许多被拦截下来的上访者，就是许志永会帮助的那种人。他们居住的条件很恶劣，看守他们的人也很凶暴，偶而还会强奸其中弱女。但许志永明明知道这种情况，却还要对香港来的女学生说「要对政府有多点耐性」；这只是因为他太善良了。<br />
<br />
 <br />
<br />
如今，好人谭作人和许志永终于消失了，剩下那批上访者还在「聚源宾馆」里面呼救呻吟。半夜，他们唱歌，希望引起外头的路人注意。据说他们唱的是《国际歌》，而中南海就在五公里之外；据说他们唱的是《东方红》，而毛泽东纪念堂就在五公里之内。歌声由激愤渐转凄楚，终于泣不成声；而街灯，兀自孤冷地亮着。<br />
<br />
 <br />
<br />
我不知道你回去之后会如何报告，你明白，中国人是不信邪的。我也早就背弃了你和你所代表的一切。如今，我将留在这里等待利维坦卷起的巨浪迎岸而来。<br />
<br />
 <br />
<br />
我另一个朋友，台吃u家杨照，曾经在《十年后的台湾》里写下这么一段我屡次引述的话：<br />
<br />
 <br />
<br />
「我还记得，我清楚记得，自己年少时候，被美丽岛事件与军法大审震，I受到那股v史性的悲「小民h威囿w制像b怪F，吞噬了一代又一代的民主\诱摺Ｇ按被拆吃入腹了，@b怪F想：不再有人敢`逆我意志了吧。不，新一代的人又⒄驹诠肢F面前，即使明知⒊橄乱奚者，即使内心害怕得身l抖，也是得挺身站在那e。因椋怪F吞噬，是惟一能蜃灾髯龅氖拢也是惟一能蜃灾髯龅氖拢也是唯一能蛟^明我自主意志尚存的幼鳎不能放」。<br />
<br />
 <br />
<br />
他接着说：「我从来不曾自认是个勇敢的人，然而在那一刻，却悲剧性地预见：等时机到了，我这一辈的人，会接上民主的棒子，克服自己的怯懦与犹豫，去站在怪兽面前，被无所不在的极权系统监视、追捕、入狱」。<br />
<br />
 <br />
<br />
我知道自己不是善人，但我寄望自己能够通过那未来的试炼，证明自己。所多玛，一座恶贯满盈的城市，它的善人皆以其自身的消亡来证明这里仍有善人。<br />
