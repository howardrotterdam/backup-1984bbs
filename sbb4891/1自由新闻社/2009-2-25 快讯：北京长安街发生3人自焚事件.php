													<h2>新华网：新疆上访者在北京王府井自焚 原因现已查明</h2>
						
						
																											<div id="postmessage_47320" class="t_msgfont">2009年03月07日14:42 　 新华网　 　 评论0条<br />
<br />
十一届全国人大二次会议新疆代表团6日举行团组开放会，应记者要求，向中外媒体介绍了日前发生的“王府井自焚”人员情况。 <br />
<br />
2月25日15时许，在北京王府井与长安街交接口处，3名新疆人驾驶一辆汽车点燃自焚。<br />
<br />
新疆维吾尔自治区党委副书记、自治区主席努尔・白克力说，在北京自焚的3位新疆人是上访者。他们采取了过激行为。这是我们不愿意看到的。<br />
<br />
据介绍，3月1日，有关方面已安排这一家3口乘坐航班回到乌鲁木齐。目前他们在乌鲁木齐友谊医院接受治疗，没有生命危险。<br />
<br />
乌鲁木齐市委书记栗智介绍说，上访人叫买买提・乌守尔，是乌兹别克族，他们一家四口人，其中三人是残疾。<br />
<br />
栗智说，乌守尔妻子的爷爷1959年前在乌鲁木齐有11间房子，共159.14平方米。按当时政策，100平方米以上就属于改造范围，因此全部按照政策改造成了直管公房。<br />
<br />
2002年乌鲁木齐把直管公房腾空，准备拆掉建学校。趁这个机会，乌守尔召集十几个人占了35套直管公房并强行出租。他们的行为不合理也不合法。<br />
<br />
栗智说，在这样的情况下，为了建学校，有关部门对他做工作，进行了再征迁。学校建成以后，他为了要回当时占去的房子，从2002年开始就不断上访。有关部门做了大量工作，但他对安置情况不满意。市委市政府又安排给他一套经济适用房，他自己看后表示满意，但突然又提出3条不合理要求：第一，要求市政府报销他所有上访费用；第二，要求市政府在媒体上向他公开道歉；第三，要求市政府给他的两个孩子安排工作。<br />
<br />
努尔・白克力说，这3条要求不现实也不合理，乌鲁木齐市政府经过研究后，拒绝了他的要求。在这种情况下，他们2月23日开车从乌鲁木齐到了北京，进行自焚。<br />
<br />
“出现这种事情我们也感到很痛心。我们全力做好善后工作，积极救助3位自焚人员，同时对他们下一步善后工作和生活作出合情合理的安排。”努尔・白克力说。(常璐 李惠子 张旭东)<br />
