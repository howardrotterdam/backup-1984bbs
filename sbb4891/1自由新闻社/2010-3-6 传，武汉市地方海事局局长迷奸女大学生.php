　　 2006年的8月份的某一天,那天是我的20岁生日.下课之后,我一个人来到位于江滩附近的一家酒吧.想为自己庆祝生日.令我没想到的是,第一次一个人过生日却留下了一辈子无法洗去的阴影.来到酒吧后,我点了一瓶饮料,坐在圆桌旁喝着饮料,听着音乐,看着舞池的人尽情的摇摆,这时从我对面走过来一个年约 40的中年男子,手里拿着两杯酒,感觉像是要跟我喝酒的架势,我顺势起身准备走的,却被他一把拉住,当时我势单力薄,也就被他的强势所压了下来,心想就是喝酒而已,没事的,大不了找个理由闪人.他一过来就说了些肉麻的话语,意思是让我喝了这杯酒,不然的话会让他在他的朋友面前没有面子,我想一杯酒应该没什么的,就答应了他,一口干了那杯酒.当时觉得没什么,可以坐着坐着,发现头有点昏,有点想吐的感觉,就去洗手间,当我走到洗手间的门口的时候,头越来越昏了,当时我昏了过去,只记得朦胧中有个人把我抱了起来.后来的事情就不记得了. <br />
