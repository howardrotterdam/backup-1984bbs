																											<div id="postmessage_205461" class="t_msgfont">2009年8月14日 星期五 <br />
&nbsp;&nbsp;<br />
四中全会前！薄熙来大打出手 终引太子党众怒 <br />
&nbsp;&nbsp;<br />
姜平 <br />
&nbsp;&nbsp;<br />
中共定于9月份召开党的十七大四中全会，主要是安排下一届的人选。从目前的安排和年龄来看，政治局委员、重庆市委书记薄熙来下届进政治局常委会是决没有希望，这让野心日日上涨的薄急到头昏脑胀，东出一拳、西踹一脚，终于引发元老和太子党们众怒。<br />
● 薄熙来的竞争对手是太子党<br />
<br />
胡温都已经就任两届，总书记、「国家主席」和军委主席，还有总理等重要位置，都要易人。元老军头们当然要安排自己的子孙继位，高层最重要位置2007年的十七大召开时已经安置好了，而且新人正在「实习」。<br />
<br />
2008年8月23 日的那篇文章《薄熙来与Xi Jinping，哪个更应该是未来的政治明星？》把薄的野心展示无遗。文章说，「对于同是太子党，又同在政治局，从学历上来说，薄熙来应该更胜一筹的，但是为何一个被选举为国家副主席，而另一个却成了重庆市市委书记了呢？选举是否还依据了其他的什么标准？」「中国的公民，如果在未来我们通过一定的努力要回了《中华人民共和国宪法》所赋予的选举权的一部分权力，也就是只能在他们两人中选出一个我们的国家主席，我们会选择谁呢？」 <br />
<br />
所以，薄熙来唱红歌、发红色短信、把别人的关系户都送进监狱，其实都是为了打击别人抬高自己，为四中全会与其他太子党争夺下届权力积攒「政绩」。<br />
<br />
一位在文革中受过很大迫害的元老说：薄熙来比他爹薄一波还狠，薄一波对救命恩人Hu Yaobang恩将仇报，薄熙来对他自己亲爹就能落井下石。这种人怎么可以重用？他到重庆才一年多，那里一天都没有消停过，监狱里都装不下，听说还天天施用酷刑。一个小小的重庆就让他搞成这样，要是让他管理一个国家，那比毛主席的「七、八年再来一次」还厉害。我们这把老骨头还不让他给折腾散了？！ <br />
<br />
● 薄熙来扫荡前任人马<br />
<br />
在官场上，上至Hu Jintao、Wen Jiabao，下至自己的所有前任，甚至于每个国务院副总理，薄熙来都骂遍了。原来薄熙来把主攻对象定为Xi Jinping，并想拉拢已调任广东省委书记的前重庆市委书记汪洋支持他进政治局常委会，所以对汪洋嘴下还有「留情」。<br />
<br />
后来，薄熙来四处打听，听说自己四中全会「没戏」，而政治局常委贺国强推荐汪洋「再上一上」时，顿时醋意大发，对从辽宁调来整人的残暴公安局长王立军说：「除了咱自己人，给我把重庆市踏平！」<br />
<br />
王立军的亲信透露，为了在9月四中全会召开前，把其前任都「镇住」，薄熙来准备抄一条近路，那就是把不靠拢自己的大批重庆亿万富翁以「当地黑社会老大」名义抓捕，然后施以酷刑，让他们「供出是如何与前重庆市委高层互相勾结违法乱纪」的，并把材料立即上报中央，堵住贺国强的嘴，打掉汪洋的升官梦，并让现任所有重庆市委干部个个自危，只顾保全自己，而不敢对薄熙来的做法提出一点反对或不满。<br />
<br />
另外，那亲信还透露，「薄书记命令先搞高法和司法局，这样想审判谁都不会遇到麻烦。」<br />
<br />
于是公安局长王立军先拿下重庆高法的张副院长，让高等法院人人恐惧，然后立即对在北京参加全国司法厅长会议的重庆市司法局长文强下手，把司法局一步到位换上自己人。为了怕其前任出面干涉，薄熙来命令，「我说的，特殊情况下，可以先不审判，先把消息发出去」通报全国。<br />
<br />
● 贺国强向Jiang Zemin告状<br />
<br />
现任中纪委书记、政治局常委贺国强当重庆市委书记时，重庆给江送去几个有点小功能的「气功师」，于是江把小小重庆市从四川省分出来，级别上成为直辖市，这样贺国强才可够资格直接进入中央。薄熙来如此一闹，让贺国强气愤难抑，说让江「出来为自己作主」。<br />
<br />
● 汪洋要求政治局开会<br />
<br />
政治局委员汪洋也请求政治局开会，「为薄熙来的不当行为」讨个说法。汪洋的理由是，「在建国60年大庆前夕，薄熙来把重庆市搞的风声鹤唳的做法很不恰当，如果中央不制止，各省各地谁想怎么闹就怎么闹，那后果是不堪设想的。」<br />
<br />
