 <br />
<br />
据现场救人者、送奶工胡亚峰介绍，由于凶手徐玉元相对于幼童十分高大，所以砍伤都在头部与颈部，皮肉翻开，气管外露，其惨状几不可睹，不少孩子看上去当时就不行了。而人民医院的一名护士长也告诉笔者，作为医院的员工，她都没有确切的消息，只能据经验判断有孩子伤重死亡。专业人士都作这样的判断，何况在现场看到惨状的百姓？以他们为源点的信息，快速通过手机、电话、网络传播开来。<br />
<br />
中国新闻社、中国之声等在上午11时迅速发布了简讯，随后正式的官方消息证实32人被砍，称疑凶被擒，无人死亡。因此前已发生多起此类幼儿园伤害事件，泰兴立即变成全网热议的话题。<br />
<br />
但在当晚的泰兴新闻中几无报道案情，而在第二天的江苏本地最大报章《扬子晚报》上，也是一笔带过。但此前报导福州屠童案时，扬子晚报用头版大幅报道。这对于如此重大的事件，并且在当地已是充分传播的事件，在老百姓看来是多么的不正常。与此同时，中宣部的禁令传达，各媒体已在路上的记者要么回家，要么去上海看世博。<br />
<br />
官方消息缄默，而各种各样的猜测就开始广泛传播。30日上午在幼儿园门口聚集的百姓对伤亡各执一辞，但如果有人援引政府所说的无人死亡，立即会被大家反驳。在街头甚至传单都出现了，在医院旁的电线杆上有传单称“截至昨日，已经有5名儿童死亡，重伤者也危在旦夕。”<br />
<br />
相对于官方消息简约冷漠，小道消息有血有肉，合情合理，更让人接受和相信。<br />
<br />
 <br />
<br />
侏儒家长医院陈情引发同情<br />
<br />
 <br />
<br />
事发第二日中午，大部份轻伤的儿童已经见到了家长。但重伤孩子的家长还没有能进入icu看望孩子。这使本来应是信息核心的家长，也开始相信传言。<br />
<br />
辛峰是其中一名重伤儿童的家长，他身高只有1米45，好不容易组成了家庭，没想到又遇到惨案。本来没到儿子就很狐疑，医院在30日下午进行第二次手术时，突然要他们签字。<br />
<br />
“为什么第一次手术不要签字，第二次手术却要签。是不是孩子已经死了，想变通到医疗事故死，以掩盖一开始的无死亡通报。”下午5点，他与外地妻子在医院门举起了写着“还我儿子、给儿治疗”的纸牌子，妻子向围观群众一遍又一遍的讲述着自己的遭遇。医院门口开始拥堵。<br />
<br />
随后，辛峰等家长来到泰兴最繁华的鼓楼广场拦住一辆政府的车辆，在上面用红纸写上了还我儿子。人群因好奇聚集起来，又因无进展而离开，但无数的手机拍摄已把消息传了出去。现场始终维持在数百人左右，鼓楼广场的群众情绪在发酵。<br />
<br />
笔者注意到，警方虽然来到现场，其间个别警察与聚集群众虽有言语冲突，但官民并没有动手。警方反而在远方疏导道路。政府在7时许派员与家长交流，承诺由一位副市长代为签字，人群让车辆开走，家长也离开了。据笔者向辛峰了解，他于当晚已经被安排见到了在ICU的孩子，还没脱离危险。<br />
<br />
但人群聚离，后来者并不了解最先进展。<br />
<br />
 <br />
<br />
中学生发动游行抗议政府<br />
<br />
 <br />
<br />
泰兴属泰州，地处江北要冲，自古多战事，岳飞在这里抗金，元初、明初都被灭城，当地百姓多明初从苏州迁来，苏州是朱元璋死敌张士诚的称王地，泰州是张士诚的家乡，所以可以说当地人是流放者的后代。保守本份、不惹事不折腾是此地人的典型性格，外出打工也多以建筑业为主，少有广东福建那样的大开大阖。大部分时候，他们是沉默，甚至是冷漠的。如果没有人打头，是闹不起来的。<br />
<br />
但是还有青年。<br />
<br />
“我是**中学的高三*班的***，大家听我说。”晚8时左右，一个身着白色运动衫的少年对着鼓楼广场的人群大声呼喊，“我们要到医院去，让家长见到孩子，让他们公布真相！”<br />
<br />
“去医院，见孩子。”分散的人群第一次喊出了统一的口号，人群不由欢呼起来，开始向白衣少年集合，此时天色已暗。<br />
<br />
白衣少年背对前方引着人群向医院走，他边退边高举着手上的矿泉水瓶。“你们必须知道我的名字，我为我个人的行为负责。”白衣少年大声喊出自己的名字，然后说，“我们的游行是正义的社会运动，不是闹事，我们要的是真相，我们要见孩子。”后面跟着的人群大声重复着口号“我们要真相，我们要孩子。”声势一下子壮了起来，路的两边不断有人加入进来。近20米宽的街道挤满了人，浩浩荡荡的走向医院。一些网友制作了“宝宝回家”的牌子，原本自发去幼儿园祈祷的，也加入到了游行队伍。<br />
<br />
警察并没有跟在队伍中，但不少政府的便衣跟随，他们并没有阻挡人群，但开始打电话找这个少年的家长。泰兴并不大，在队伍行进到离医院只有50米的时候，一个疯了一样的中年妇女冲到白衣少年面前，一把拽住。“回家，我们回家”。边上的群众喝道，干什么？“我是他妈，你们不能拿我儿子当挡箭牌啊。”边上的群众顿时语塞。<br />
<br />
父亲和母亲拖着白衣少年离开人群，少年转过头，夜市的霓虹灯照亮了他倔强的脸。<br />
<br />
 <br />
<br />
政府用媒体轰炸平息游行<br />
<br />
 <br />
<br />
人群已经聚了起来，这段插曲并没有影响到进程，走在队伍前面的几个人叫喊到，我们自己去医院。<br />
<br />
人民医院大楼前是个数千平方的大广场，本来有门关着，冲在最前面的是几个年轻人把门推开，很快人群就挤满了广场，医院的玻璃大门也在拥挤中不知道给谁砸碎了，一地的玻璃珠。由于医院的电梯已关，再加上没有人肯打头，冲进去的人们并没有能上到21楼的病房。<br />
<br />
10时左右，人民医院大楼上的电子大屏幕开始打出字幕：广大市民，我院负责任地告知大家，429案件中受伤的29名儿童和3名成人，到目前为止无1人死亡。请广大市民不要听信谣言。现场群众有的人狐疑，有的人大骂，但起到了作用，人群逐渐离散。<br />
<br />
现场11时左右，泰兴市常务副市长孙云到了，他和市委秘书长给人群喊话。请大家回家。还请来了人民医院的副院长以及一名家长，讲了救治过程。说今天刚有两人离开重症回普外，刚刚又做完一个手术，还有四个在重症。在这之后，游行就基本结束了。<br />
<br />
第二日五一节一早，泰兴几乎所有市民都接到了市政府的短信，称所有家属都已见到亲人，请市民相信政府，不要听信谣言。在事发地中心幼儿园、市人民医院都告示出了这样的信息。居委会大妈今天都在社区内的来回工作，劝大家不要上街。与此同时，泰兴电视台开始滚动播出新闻，播出家长见孩子的画面，并由孙云副市长通报了祥细的案情，反复播出，不插广告。对此市民的评价是“早干嘛去了”。前一天晚起了大作用的人民医院大屏幕上，还写出了“维护稳定人人有责，扰乱社会秩序，将依法严肃处理。”<br />
<br />
在这样的信息轰炸之下，泰兴的街头已经看不见昨天的景象。人民医院的秩序也恢复了正常，好像没有发生过什么，只是大楼门脸上，那没有门的门洞像缺了一颗门牙。人民医院大屏幕的信息是滚动的，一会儿，跳出了“热烈庆祝上海世博会开幕”。也许，这就是当地一开始想低调处理事件的原因所在。</div>

							
							
							
							
