													<h2>中南海动态：政治局已经吵成一片</h2>
						
						
																											<div id="postmessage_283632" class="t_msgfont">中南海动态：政治局已经吵成一片&nbsp;&nbsp;<br />
――原标题：[中国大陆] 知识界强烈要求政改 <br />
打印版 【 阿波罗新闻网2009-10-14讯】 作者：何嘉峪&nbsp; &nbsp;<br />
<br />
 外界普遍认为，九月下旬召开的中共十七届四中全会主调是定十八的人事盘子，但是，面对知识界普遍要求政改的愿望，当局无法回避。有消息表明：是否讨论政改，在政治局已经吵成一片……<br />
<br />
在六十年大庆来临之前，当局采取特强政治高压措施，引起知识界的普遍不满，认为当局在政治上已经出现大倒退，或将意味着十八大会出现「新勃列日涅夫政治」。<br />
<br />
即便中共内部比较左的机构也发出示警信号，告诫权力核心层：如不政改，必将会引爆社会性骚乱。「难过的关口不是六十年大庆，难的是大庆平安搞完之后，我们能否解决积延已久的政改问题！」中央党校一位知名教授写给胡锦涛本人的报告，很直白地把问题摆到桌面上。<br />
<br />
一报一刊委婉批胡<br />
<br />
中央党校那位上书胡锦涛的教授的意见不是孤立的，进入八月以来，较为开放的纸媒和网络都在「从历史经验的角度促动政改」。一位研究中国当今政治趋势的民间学者如是分析。一直在经济学家阶层受欢迎的《经济观察报》，于八月十日发表中国政法大学金雁教授的考察报告《前东德：波澜不惊二十年》，赞扬东德放弃专制、并入西德，实现社转轨的政治效绩。文章大胆地说：「东欧的绝大多数国家已经走向复兴，社会稳定，经济持续增长，尤其是已加入欧盟的那些国家，已经开始了一场新欧洲追赶老欧洲的比赛。」重量级的民间研究人士认为，这一论点代表了体制内主张政改的知识分子对胡「不易帜论调」的柔性批驳。<br />
<br />
社科院世界历史研究所黄立教授发表于《南风窗》二九年第十七期上的文章《苏联因何丧失改革良机》一文，则被上述人士认为是警惕中共十八大出现「新勃列日涅夫」的信号。文章说：「在政治领域，由于利益集团的抵制，改革更难推动。勃列日涅夫时期，领导干部的腐败之风盛行，严重影响了党的威信，反腐败成为重要的政治任务。但是，反腐行动却遭到总书记勃列日涅夫的阻碍。」据北京接近权力核心层的体制内专家，被邀请给政治局讲课之后说：习近平接任总书记后，很可能重复江泽民「以腐败换团结」的路子，连胡温现在高喊的反腐调子也不喊了。<br />
<br />
推进政改会否牵涉到敏感的民族问题，是中共权力核心层无解的难题。陪同胡锦涛在八月下旬视察新疆的书记处书记、中央办厅主任令计划，在回京途中对胡讲：「苏联解体时，我们庆幸自己没有苏联那样棘手的民族矛盾，现在看来，当时的乐观是盲目的！」胡听此言，沉默不语。<br />
<br />
百万武警不堪用<br />
<br />
体制内知识分子强烈要求政政，亦是出于自身利益的考虑，因为一旦大规模社会骚乱导致社会崩盘，他们难免殉葬的命运。面对越来越多的上访，研究人士预计在明后两年内很可能出现「天安门广场重新被占领」的现象。「这次不是学生，应该是数以百万计的访民。我相信，没有人敢下开枪的命令。」一位军内的中阶社情分析专家如是说。<br />
<br />
为了对付可能的骚乱，也为了逐步消除公安系统的防暴特警滥权，胡锦涛决定赋予军队参与国内维稳的职能，同时推动武警法的立法进程。消息人士称：到二一二年，中国的武警员额将达到一百万人，和军队的比例为一比一点六七。但是，民间分析家认为面对因社会严重不公、司法极度腐败、政府公信力丧失的诸种难题交织之状，民间大规模骚乱发生的可能性日渐增大。武警和军队的内部腐败又烈于社会，很难形成真正的动员能力。<br />
<br />
一位知名历史学者在要求匿名的前提下，讲了「古中今外」的两个案例：第一是，秦朝灭亡前，南方五十万大军（骑兵占三分之一）为何拒不救亡？原因是军队由平民组成，他们是腐败与暴政的受害者，秦朝灭亡是他们乐见之事，高级指挥官奈何不得；第二是，波兰圆桌谈判发生之前，团结工会领导人对雅鲁泽尔斯基的军政府不屑一顾，并公开指称后者「除了军队，你什么都没有」，即完全丧失了民心。事实也证明，在党控军队的前苏东国家里，军队在剧变中并没有支持所谓的党的领导。<br />
<br />
县长民选书记京派的「新思维」<br />
<br />
体制内知识精英要求政改的呼吁声不断，亦在技术层面提交研究报告。据悉，北京附近某省的一位行政学院院长向政治局提交了《县长民选、书记京派的县政改革模式建议》，建议得到胡温信任的北京大学知名法学教授、政治理论专家巩献田的首肯。<br />
<br />
在目前进行「省管县」、「县委书记升格」的实际操作中，尚没有任一个地方启动县长民选程序。主要的思路还是如何加强中央通过党权对基层的控制。在「省管县」暨县委书记省派的思路基础，海外支持胡温的专家学者充分肯定「京派书记」的模式，认为这将实现中央对基层政权的控制，避免中共基层政权黑社会化。不过，上指的历史学者称：「就是中央委员出任县委书记，也无法解决中国吏治败坏的历史习性，除非痛下决心，实现选举民主。」<br />
<br />
给县委书记升格的政治操作，是政治局委员、中组部部长李源潮的形象工程，得到了党内理论权威们的大力支持。中央党校的一位教授在内部谈话时说：要把中央党校办成「新的黄埔军校」，不断培训县委书记和县长。也有体制内人士称：这种梦想不切合实际，因为无论县委书记京派还是京训，最后这个机制都会成为党内不同派系争夺的资源，实施中会变形；还有，没有任何办法保证京派书记不被地方习性同化，不被「历史黑洞」吞噬。湖南省委书记张春贤是公认的解放思想领军人物之一，更是不遗余力地实施李源潮的形象工程。但是湖南县委书记层面还是整体灰化，以至于他在一次座谈会上「哀求」县委书记们「少上电视，多干实事」，云云。<br />
<br />
万里谈话顺应民意<br />
<br />
另据消息人士称：万里谈话是顺应民意的大手笔，而且党内有不少中高级官员在私下里均表示「应当学习波兰，通过圆桌谈判，实现中国社会低成本转型」。该人士还透露：「应权力核心层的智囊机构之邀，社科院俄罗斯东欧中亚研究所提交了一份详细介绍波兰谈判转型的评价报告。」但是，该报告是否真地能印证应当学习波兰的意见被重视，尚无法证明。<br />
<br />
九月下旬，中共将召开十七届四中全会，为十八的人事安排定盘子。体制内知识精英的政改诉求对胡锦涛等人形成了巨大的压力，政治局内对是否讨论政改问题，发生了严重的意见分歧。严重分歧更影响到军队，军队内部主张改革的力量，建议干部财产登记制度应在军队率先推开，特别是对新提拔与重点培养的干部，要充先公布个人财产状况。反对意见则称政改会动摇军心，现在「要大力防止网络颠覆力量的破坏」，实乃王顾左右而言他之策。<br />
<br />
（原载《动向》杂志2009年9月号）</div>

							
							
							
							
