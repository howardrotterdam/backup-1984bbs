资料来源：《苹果》资料室 <br />
<br />
<strong>传万里斥中共瞎折腾 <br />
「人民有话让他说」 </strong><br />
2009年10月12日 <br />
<br />
今年8月、中共建政60周年前，海外传流一份万言书，声称是中共元老的谈话，对中共拟大事庆祝60年庆典，提出批评，包括庆典宣传不讲真话、 60年来「瞎折腾」至今未了、民意处理失败、打压不同声音等等。有传该万言书为万里的谈话记录，但本报无法证实。 <br />
<br />
<strong>最大失误处理民意 </strong><br />
<br />
在这篇万言书中，主人公一开始就表示，「建国60周年，听说正忙着阅兵准备，我已经老了，腿脚不灵，去不了天安门城楼了」，并批评为庆典进行大规模宣传报道，「是60年来老办法，一直没变过」、「不能光宣传辉煌成就，也要让人公开讨论一些问题。」 <br />
<br />
文中指，中共有7,000多万党员，但至今没有在社团管理部门登记。党国不分；党库与国库不分；党军一体；党内60年都没有建立真正意义上的竞争选举制度；各种运动不停，瞎折腾，祸国殃民。文中还指，中共60年来最大失误是民意处理失误，「人民有话就让他说，不要大惊小怪，不要乱查封杀」，「当年国民党封杀我们，历史证明他们失败了。我们绝对不能用类似手段，对待不同意见，对待其他人士。」 <br />
<br />
最先发表该篇万言书的海外新世纪网主编、曾在内地官方喉舌任职的张伟国指，他从国内某著名知识分子收到该万言书，并确认文中讲话的元老就是万里；赵紫阳前秘书鲍彤也认为感言出自万里。 <br />
<br />
<strong>人物介绍 <br />
邓小平头马反对镇压民运 </strong><br />
O果日, 2009年10月12日 <br />
(4人)浏览人次：5,533回应：0转寄朋友文章回应 <br />
万里虽不是中共最早期元老，但因他于上世纪七、八十年代在中国农村首推改革开放，与赵紫阳一道，成为当时邓小平改革开放的两匹头马，从而令他获得中共八老之誉。除他在生外，其他七老包括邓小平、陈云、杨尚昆、薄一波、宋穷、习仲勋、邓颖超，已先后逝世。 <br />
<br />
<strong>回国支持赵紫阳被阻 </strong><br />
<br />
六四时王维林无惧镇压，用身体挡坦克。资料图片 <br />
<br />
万里1936年才加入中共， 50年代调北京副市长，曾负责人民大会堂建设；文革时被打倒，至1975年邓小平短暂复出时，起用他接掌铁路，整顿当时乱成一团的铁路交通。 <br />
<br />
文革结束，邓小平真正掌权后，万里在安徽打破毛泽东遗留的人民公社体制，推行分田包产到户，打开中国农村改革开放大门，成为邓小平改革开放政策有力支持者，与时四川省委书记赵紫阳齐名，民间有「要吃米，找万里；要吃粮，找紫阳」之说。 <br />
<br />
1989年六四时，时人大委员长的万里支持赵紫阳，反对武力镇压民运。其时，他结束外访要回国处理事件，但专机到上海时，邓小平指使当时上海市委书记的江泽民将他滞留，令他无缘回京加入赵紫阳阵营，六四后避过处分，直至1993年退休。 <br />
