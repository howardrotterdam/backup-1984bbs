<strong>万里小档案 </strong><br />
&nbsp;&nbsp;<br />
年龄： 93岁（ 1916年出生） <br />
原籍：山东东平 <br />
简历： <br />
1936年加入中共 <br />
1966年文革期间被打倒 <br />
1977年安徽省委书记 <br />
1980年国务院副总理 <br />
1982年中央政治局委员 <br />
1988年全国人大委员长 <br />
1989年六四时，支持总书记赵紫阳反对武力镇压学运 <br />
