<br />
【中国企业家网】据彭博社报道，哈佛大学经济学教授、国际货币基金组织(IMF)前首席经济学家肯尼思・罗格夫(Kenneth Rogoff)日前表示，中国房地产市场即将崩溃，进而对银行业造成沉重打击。<br />
<br />
肯尼思・罗格夫表示，随着中国的经济发展，特别是以目前的增长速度，必然会出现跌宕。此外，他还认为，尽管目前世界经济在以非常缓慢的速度复苏，但是出现衰退的危险并未排除。<br />
<br />
肯尼思・罗格夫的观点与中国股市暴跌势态交相呼应。中国的数据一直饱受关注，因为中国被视为全球经济从战后最严重的衰退中复苏的领导力量。<br />
<br />
中国一季度GDP增长11.9%，政府尽力给经济降温，同时减少房地产市场投机。央行今年拟削减22%的信贷增长至7.5万亿元。<br />
<br />
这些措施导致房地产市场交易低迷，而物价则持续攀升。<br />
<br />
“你们将看到房市崩溃，这将打击银行系统，”肯尼思・罗格夫表示。他还认为，尽管有许多有效的工具和管理，但化解房市崩溃带给银行业的冲击并非易事。<br />
<br />
上周，由于中国实行货币紧缩措施，高盛下调了对中国经济增长的预期，从11.4%下调至10.1%。<br />
<br />
肯尼思・罗格夫还表示，期待中国向世界其他国家的出口以现有速度增长是不现实的。“那是不可能的。在某种程度上，为了经济持续增长，他们必须调整自己的政策。”</div>

							
							
							
							
