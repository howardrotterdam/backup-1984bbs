													<h2>南方报业收购美国《新闻周刊》遭拒</h2>
						
						
																											<div id="postmessage_713772" class="t_msgfont">《纽约客》<br />
<br />
南方报业集团尝试收购《新闻周刊》（Newsweek）遭拒的消息让双方都感到深受侮辱。《新闻周刊》无法想象被全球囚禁记者数量仅次于伊朗的国家的公司收购，美国传媒界则不愿面对其产业不断滑坡，并沦落至出售以求生存的现实。南方报业集团被《新闻周刊》当做不合法的竞购者伤透了心。在接受安徽省《合肥晚报》采访时，《南方周末》执行总编向熹说：“我个人觉得原因较复杂，但有一点，卖方不真正了解中国有理想的媒体人、媒体机构的愿望。” 《南方周末》是南方报业集团旗下一份广受欢迎的报纸。<br />
　　向熹在采访中说：“即使收购《新闻周刊》(《Newsweek》)项目失败，也将继续物色投资对象。”他希望“有国际影响的传媒机构” （有引入中国大陆股东意愿的）能有兴趣去跟他联系。他甚至提供了一个他的邮箱：<a href="mailto:xiangxi100@hotmail.com">xiangxi100@hotmail.com</a>。3<br />
<br />
<br />
The fact is that neither side truly understands the other in this case. The prospective buyers are not wrong that they have a right to bid on an American news organization, but they are wrong that they had the remotest shot of succeeding. For the moment, the spiritual gap between them and American news organizations is larger than even the most sober Chinese media baron probably imagines. A sale of this kind is, for the moment, beyond imagination. <br />
这件事情的困境在于双方都没有真正的理解对方。潜在的买主自然有权利去竞购，但是他们却忽略了他们离成功的距离还有很远。事实上，即使南方报业集团是中国最冷静的传媒大亨，但他们依然无法想象两者之间精神上的差距。就目前而言，这类收购还无法想象。1<br />
But it’s also true that Xiang Xi is not being cynical when he says that most Americans don’t grasp the “desires of idealistic Chinese media workers.” Much of the discussion about the Newsweek bid has been about the Chinese government’s campaign to project “soft power” abroad, and that is relevant. But that is also losing sight of some nuances in the world of Chinese media. Like every newspaper in China, those in the Southern Media Group are owned by the state, and the party appoints the top editors. But the Southern Media Group is not the People’s Daily, and the differences are worth acknowledging. <br />
但有一点是肯定的，向熹认为“卖方不真正了解中国有理想的媒体人、媒体机构的愿望”并不是只是愤恨之言。大多数关于竞购《新闻周刊》的争论都认为这是中国政府企图向海外推广“软实力”或类似目的的措施。但是这些争论都忽略了中国传媒业的些许微妙之处。南方报业集团确实如同其他中国媒体一样，由政府指定总编进行管理的国有公司。但是南方报业集团并不是人民日报，他们之间的区别还是需要有所区分的。<br />
Philip P. Pan, in “Out of Mao’s Shadow,” describes it this way: <br />
Philip P. Pan在《走出毛的阴影》（Out of Mao’s Shadow）中是这样描述南方报业集团的：<br />
The Southern Newspaper Group was an oasis of open-minded thinking in the state’s hidebound media empire, and it was known for pushing the limits within the propaganda apparatus. Its most daring publication, a paper based in Guangzhou named the Southern Weekend was winning readers and inspiring journalists across the country by showing how aggressive reporting and elegant writing could be possible despite censorship. On the Communist organization charts, the Group was part of the propaganda bureaucracy, but it also occupied a special place in the informal web of interest groups that made up the party. Ideologically, it was a camp for the party’s liberal wing. The editors of its newspapers were heirs to a tradition that began in 1957 during the Hundred Flowers Movement, when their predecessors launched a paper that gave voice to opinions that differed from the party line. One of the founders … argued that even if political conditions made it impossible for journalists to always write the truth, they should at least refuse to publish lies. Generation after generation, the editors of the Southern Newspaper Group tried to live up to that standard.”<br />
在中国墨守成规的媒体版图里，以思想开放著称的南方报业集团是一块绿洲，在身为宣传工具的同时，却也在不断试探底线。旗下最大胆的媒体叫《南方周末》，它证明了报纸在受到严厉审查的情况下，仍然可以进行客观公正的报道，保持优雅的文风，并因此赢得了读者的欢迎，也激励了各地的记者。在中共的组织架构中，南方报业集团是宣传系统的一个组成部分，但在党内有着一张各个利益团体结成的网络，这个集团在这个网络里同样占据着一个特殊的地位。从意识形态来看，它是中共民主自由派的阵营。自从1957年开展的百花齐放运动以来，南方报业传媒集团的前任领导就开始创办与党内主流声音有所不同的报纸，即《羊城晚报》。其中一位创始人强调说，假如政治气候不允许记者永远讲真话，那么，他们至少应当拒绝说谎话。一代又一代，南方报业传媒集团的编辑记者们都在努力达到这个标准。<br />
In 2003, one of the Southern Group’s editors, Cheng Yizhong, dared to publish an investigation into the death of Sun Zhigang, a young graphic designer who died in police custody after a beating―a report that led to the discovery and closure of a nationwide network of seven hundred underground police-run detention camps―an astonishing case of the Chinese press influencing national policy. But, as Pan wrote, Cheng was detained for doing it; he spent five months in a detention center and now works in a low-profile media job. His two colleagues were sentenced to long jail terms. Before he went to jail, he gave a speech to his staff that included this line: “Whatever happens, we must not give up the values and beliefs of the Southern Metropolis Daily. We have reason to be proud.” <br />
2003年，《南方都市报》编辑程益中突破限制，报道了年轻平面设计师孙志刚被非法收容，殴打致死一案。这则报道揭露了700个地下收容点的黑暗，出乎意料的影响到了国家政策，并直接导致他们的关停。但是，程益中却为此饱受牢狱之灾，他被关押在拘留所5个多月，现在只能在一家不重要的媒体供职。他的两个同事则被判处长期监禁。在他被捕之前，他对南方报业集团的同仁说道：<br />
“无论将要发生什么，我们都不要迷失南方都市报的价值观。我们有理由自豪：南方都市报是一张经得起历史考验的、有存在价值的报纸。”<br />
<br />
It was a courageous sentiment from someone working in a profoundly imperfect system. I wouldn’t want to be owned by them either, and I don’t expect it will happen anytime soon. But, let’s not sneer too much. As colleagues in a business that is tough enough, Chinese journalists deserve our respect.<br />
对于一个生活在一个有待改进的社会中的人，这是一个充满勇气的想法。我不想《纽约客》被他们中的任何一个人拥有，我也不认为这会很快发生。但是，请让我们不要对他们讥笑。因为作为在足够艰难的环境中谋求生存的同事，中国记者值得我们的尊敬1<br />
