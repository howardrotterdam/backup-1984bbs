<br />
<br />
自从中国提出要耗资上千亿元巨资，通过并购重组，打造具世界影响力的中国传媒，改变舆论弱势的战略之后，美国便高度关注，不仅加强了对中国传媒的封堵，而且加大对中国的进攻力度，美国一些重要报纸和杂志纷纷推出中文网站。<br />
<br />
<br />
