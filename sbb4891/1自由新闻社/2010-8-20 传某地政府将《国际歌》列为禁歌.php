<br />
<strong>《国际歌》成为禁歌?</strong><br />
<br />
近日有幸，叨陪一个“企业职工教育座谈会”，主持者郑重透露一个“企业职工教育新动向”：某地政府为了建设“和谐社会”，防止劳资关系对立，内部下达一“新纪律”：凡是有劳工、民工参加的企业集会或社会集会，一律不许播放和呤唱《国际歌》，特别严禁传唱《国际歌》歌词，违者以“党纪国法”追究“破坏社会稳定”之罪。这就是说，曾经使“全世界无产者联合起来”的“革命圣歌”《国际歌》，今日已碍“稳定压倒一切”，不利于教育劳工、民工等“无产阶级”了。<br />
<br />
曾几何时的《国际歌》传教者，今日不得不暗暗禁止《国际歌》！何等沧海桑田，“天翻地覆慨而慷”，千真万确“新时代”。<br />
<br />
46年前，我上小学，第一支歌就学《国际歌》；40年前，“文革”开始，年年唱月月唱天天听《国际歌》；近30 年，《国际歌》与日俱远，“惟见长江天际流”；偶而路过音像店听到几句带“红色摇滚”味的《国际歌》，顿觉“国际悲歌歌一曲，狂飙为我从天落”，周身一阵“天下者，我们的天下”之热流。稍一清醒，又恐《国际歌》再成“四面楚歌”，“全国山河一片红”。<br />
<br />
尽管对《国际歌》如此又爱又怕，一旦得知《国际歌》成为禁歌，不免“我心惆怅”，“才下眉头却上心头”。<br />
今天重唱《国际歌》，是否会“破坏社会稳定”？反复重读歌词――《国际歌》的核心就是斗争――“要为真理而斗争!/旧世界打个落花流水”；就是复仇――“最可恨那些毒蛇猛兽/吃尽了我们的血肉”；就是“以其人之道还治其人之身”的“一元论”――“一旦把它们消灭干净/鲜红的太阳照遍全球。”<br />
<br />
如此“一元化”思想，的确与今天旨在顺应“全球化”、“多元化”的“和谐社会”背道而驰。 “阶级斗争”时代的《国际歌》，求之不得的是“天下大乱”，要“过七八年再来一次”，将“旧世界打个落花流水”，就是要破坏一切“旧秩序”。一些“文化低，委屈多”，倍受不公正待遇的民工，一些被骗惨死的矿难工人的家属，有可能被《国际歌》激动得失去“理智”。</div>

							
							
							
							
