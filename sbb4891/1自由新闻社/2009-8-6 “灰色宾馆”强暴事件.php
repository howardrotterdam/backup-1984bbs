													<h2>“灰色宾馆”强暴事件</h2>
						
						
																											<div id="postmessage_192335" class="t_msgfont">“灰色宾馆”强暴事件 <br />
来源：南方周末 　2009-08-06 09:35:07 　作者：朝格图　杨继斌　　<br />
<a href="http://www.nanfangdaily.com.cn/nfzm/200908060086.asp" target="_blank">http://www.nanfangdaily.com.cn/nfzm/200908060086.asp</a><br />
<br />
　　在被带进聚源宾馆后6个小时,安徽姑娘李蕊蕊遭到强暴,涉嫌施暴者正是宾馆的“看守”。案发之时,已有 70多名各地来京的特殊“住户”被带进宾馆,拥挤于这家毗邻北京火车南站的灰色院落内的简易房里。此前已多次关注这家宾馆的警方在接警后立即介入,已连夜控制嫌犯。这到底是一家怎样的宾馆,人们为何被“看守”于此,在灰色的简易房内,他们有着怎样的遭遇?<br />
<br />
<br />
　　“管吃管住,解决问题”<br />
<br />
　　安徽姑娘李蕊蕊被带进聚源宾馆的时间是8月3日晚8点,确切说是被带进宾馆东侧的简易房中。当时天光逐渐黯淡,简易房内横七竖八地躺坐着几十号人,其中,34岁的王云丽刚喝过三天以来的第一顿玉米面糊糊,45岁的张建秀则困倦地躺在床上。“看着是个胆小的女孩子”。在王云丽的印象中,李蕊蕊性格内向, 被带进这里后话不多。6个小时之后,李蕊蕊在简易房内遭到强暴,多人指认涉嫌施暴者正是简易房的“看守”之一。在被关押的人们跑出宾馆向洋桥派出所报案后,刑警已将嫌犯控制并连夜进行讯问。<br />
<br />
　　综合宾馆服务员和宾馆老板的说法,聚源宾馆与河南桐柏县驻京机构有着特殊的“业务关系”。<br />
<br />
　　仅从外观上和宣传上看,这家位于北京南二环陶然桥西南角、毗邻北京南站的建筑没有任何特点,进入悬挂着大幅广告牌的大门右转,四层灰色老楼闯入眼帘。比邻的北京市民熟悉了类似的事实:两三年前开始,常有很多外地口音的人被各地工作人员从车里卸下,带进楼边的简易房中。透过家中的窗户能看到他们吃饭洗脸, 夜里会传出争吵和尖叫声。<br />
<br />
　　张建秀和王云丽被带进聚源宾馆的原因相同。她们来到北京想向有关部门反映各自的问题,随后被来自老家的工作人员找到,“找个地方,管吃管住,解决问题”。<br />
<br />
　　目前尚无从得知20岁上下的李蕊蕊是带着何种心情,又是因何被带入聚源宾馆的。进入宾馆大门右转,穿越一条20米长的走廊,被带入平时封闭的木门,她就进入到简易房内。<br />
<br />
　　木门内的世界不为外界所知。南方周末记者8月5日实地探访发现,150平米空间内,包括一个30平米左右的大厅,其他部分被分割成很多10平米左右的小间。每个小间内像大学宿舍一样摆着5-6张上下铺的床位,一些铺位上凌乱堆着破烂铺盖。而厨房和厕所紧挨着。在这里,手机信号是被屏蔽的。木门外,就是那 7个身强力壮的“看守”。<br />
<br />
　　对于这个简易房的用途,工作人员给出了截然不同的说法。一个女性宾馆工作人员说这是仓库,没关过人；另外一个男子说,这是食堂。而在一个角落,一位工作人员低声对我们说:人昨天都被带走了。<br />
<br />
　　只有打开另外一边的一扇大铁门才能见到阳光,而那个门通常只有饭后才打开一小会,因而白天房间内都亮着灯。房内空气浊重,人们不能走出这150平米的房间,也无法与外界沟通。<br />
<br />
　　按照多位“住户”的说法,这里有七十多个男女老少混住在一起,一半以上是老人,还有几个失去父母的孩子由爷爷奶奶带着。7个看守者住在大厅中,他们都是男人。其中那个叫做“小强”的年轻人看上去很帅,1.8的个子身材健壮皮肤白皙,但却经常打人骂人。8月3日当晚,李蕊蕊被安排到了大厅中紧靠门的位置, 上铺。下铺就是小强。<br />
<br />
　　“灰屋”里的噩梦<br />
<br />
　　年轻的李蕊蕊还没有时间适应这里的一切,但王云丽有。李蕊蕊的到来给王云丽带来了不祥之感。<br />
<br />
　　从7月1日到14日的两周内,这位4个孩子的母亲被关了半个月―――王云丽用“噩梦”二字形容自己的经历。<br />
<br />
　　直到8月1日第二次被带到这里前,她害怕到这里来,但选择权显然不在她手里,“他们说名额早就分配好了”,不止一个人回忆说,“适应其中的生活完全凭借个人能力”。<br />
<br />
　　除了封闭,还要适应简易房内的生存环境。仅有的几样菜是带瓤的冬瓜、咸菜和老茄子。没有枕头和被子,床上只有薄薄的一个褥子,七十多个人只有一个厕所, 排队往往要40分钟。一个年轻小伙子因为想看电视新闻得罪了一个想看电视剧的看守者,被打了一顿。一个老人因为到厨房用洗涤剂也被打了。<br />
<br />
　　王云丽挨打的理由是“门敲得太重了”。她被一个叫王学力(音)的年轻男子按着脖子推到两三米外摔倒在地。扶着床半天才起来,她又挨了两拳。有人抗议说“要告打人的人”,但打人者声音更高:“随便告!”<br />
<br />
　　本报记者获知,宾馆所属的北京洋桥派出所曾介入调查,有打人者曾被带入派出所询问。在这些进京反映问题的人们被圈禁的时光中,这是他们惟一的希望。<br />
<br />
　　王云丽也曾向警察寻求帮助,她被打之后努力争取到输液的机会,逃出宾馆外拨打了110。在警方介入后,她终于因此被调出大房,享受了聚源宾馆二楼5平米小单间的待遇。那里有被褥枕头和小电视。<br />
<br />
　　来自男性看守者的威胁让女人们更为害怕,闷热的晚上睡觉根本不敢脱衣服。第二次被带进来后,王云丽特意选择了一间最靠里的床位,外边包裹着一层男访民,“这样安全些”。<br />
<br />
　　预感还是应验了,漆黑夜里一个叫做老赵的中年看守者爬到了她的床上,她把他轰走了。老赵又爬到了另一个韩姓女人的床上,一次喝醉了还摸她。这个胆小的女人此后跟王云丽挤在一张一米宽上铺的硬板床上。<br />
<br />
　　漫长的白天有人睡觉有人聊天。其他访民大多在互相倾诉彼此安慰,实在憋闷了,大家就一起唱革命歌曲,比如“东方红”。“希望外边的人听到,帮我们出去”。<br />
<br />
　　对于新来的李蕊蕊,还没有人来得及告诉她这里发生的一切。<br />
<br />
　　沉默中的强暴<br />
<br />
　　8月4日凌晨,时间在狭窄、燠热、散发着酸臭味的空间里推移到大约凌晨两点。睡在大厅里的安徽金德人王春听到门口钢管焊成的床架有大的声响。黑暗中,隐约能看到看门的小强站在床下,往新来的女孩身上摸。“当时没有多想,以为是通奸呢。”王春说。<br />
<br />
　　在稍早一些时候,这个奇特的人群中,的确存在过“看守”和“住户”之间的“爱情”。<br />
<br />
　　据多个“资深住户”回忆,一个叫韩丽的女人在凉菜盆里挑了几粒花生米被打出了黑眼圈,一天没吃饭后想通了,不久就跟那位打了她的看守者“好上了”,由此换来被带出几个小时的优待,出去散步,购物、吃东西、打电话。<br />
<br />
　　在旁观者的叙述里,她和看守者之间的关系有些像斯德哥尔摩症(犯罪的被害者对于犯罪者产生情感,甚至反过来帮助犯罪者的心理情节)。但“恋情”的确让韩丽开始相信这里的合法性,她曾很诚恳地安慰后来者:这里免费给你们提供吃住,还帮你们解决问题,安心住下去吧。<br />
<br />
　　小强的手伸向李蕊蕊的时候,她已经睡着了。李蕊蕊事后面对镜头回忆说,被摇醒过来后,她听到小强说,走吧,出去玩。李蕊蕊拒绝了。6个小时之前,当她被拉到这里的时候,小强就已经注意到她。彼时夜幕已降,李蕊蕊独自趴在床上发呆,小强曾站在床下,拍着她的屁股,喊,美女,出去逛逛。但李蕊蕊以“我不认识你为由”,拒绝了。<br />
<br />
　　李蕊蕊到聚源宾馆的时候,晚饭已经错过。据王云丽回忆,大约10点左右,李蕊蕊曾拿钱给小强,央求后者去给她买了三包方便面。她自己留下了一包,并送给了自己的年长老乡和同时被关进来的人各一包。<br />
<br />
　　王春看到,强壮的小强爬上了李蕊蕊的床。他听到李蕊蕊叫喊的声音,他开始确信是强奸而不是通奸。但屋里其他人都和他一样保持了沉默。“听说看守有家伙, 他们人多,我们这边多是老弱妇幼,黑灯瞎火怕吃亏。”8月4日中午,王春为人群的沉默辩护。“我怕他们。”来自河南的老邢说。“小强经常打人,随便找理由打人,我们都怕他。”8月5日早上,记者在聚源宾馆隔壁的马家堡23号院了解到,临近的居民经常听到隔壁传来很大的吵骂声,“但我们都不知道里面发生了什么”。<br />
<br />
　　王春和老邢事后说,他们都看到小强爬上了李蕊蕊的床,用床单盖住自己,趴在了刘的身上。<br />
<br />
　　8月4日中午,在去派出所报案的路上,面对一个好心人的镜头,李蕊蕊对这一个过程的讲述如下:“他开始站在我床前,在我脸上亲,我推他,推不开。他亲了一会就爬了上来,又往我脖子上亲,我还推,我喊你下去,还是推不开。他把我胸前的东西都撕开。我不让他摸,他非摸。我把他的手推不开。然后他把他的裤子脱掉,把我的裤子也脱掉。”<br />
<br />
　　在镜头里,这个瓜子脸,细长眼睛的年轻姑娘语调近乎漠然,在叙述的结尾,她猛地激动起来,声音尖利,。<br />
<br />
　　事发时有人似乎听到一个老者喊,“你再不下来我用棍子敲死你”。二十多分钟后,小强下来了。老邢看到李蕊蕊从床上坐起来喊,“他强奸我,别跑”。<br />
<br />
　　“我们的事情不要你们处理了”<br />
<br />
　　绝大多数人,是在天亮之后才知道发生了什么。因为几乎每个夜晚都有吵闹、哭喊以及梦魇,所以当晚睡在里屋的人们并没有从李蕊蕊哭喊中听出什么异常。<br />
<br />
　　天亮之后,住在里面的人开始出来抱怨说昨夜外面太吵。但人们很快看到李蕊蕊白色的床单上,蝴蝶状的淡红血迹。几个目击者开始讲述强奸事件。群情开始沸腾,有人已经被关了4个月。<br />
<br />
　　李蕊蕊已经在洗手间里清洗过自己。不止一位目击者称,血迹留在水龙头下。开始,李蕊蕊担心这么多人看到自己被强奸,以后没法嫁人,决定装作什么都没发生,但前一天吃过她方便面的两个女性提醒她,“如果就这么认了,以后小强逮到你的软,会经常占你便宜”。“当时李蕊蕊呆呆的,都有些神志不清。听到一些有社会经验的妇女这么说了以后,便开始找小强哭打,抓他挠他。”王云丽说,小强当时就坐着,也不还手。<br />
<br />
　　早晨六点多,往日,已经是大家开始做早饭的时候了。但是当天没有人张罗,人们只想着离开牢笼。女人们开始在大厅的右侧拍打铁门并高喊:“有人被强奸了,救命啊。”外面没有任何声响。小强曾尝试着阻止女人们的叫喊,但他的骄横已经失去了往日的威力。“要冲出去报警,给小女孩讨一个公道。”二十多个小时后,王云丽已经回想不起当初是谁最早提出这个提议,但彼时,这是群情激奋的人一个共同的想法。<br />
<br />
　　次日,与聚源宾馆一墙之隔的马家堡23号单元楼的很多居民,听到简易房传来巨大的叫喊和打砸声。看到女人们的叫喊无人应答,男人们开始盯上了大厅另一侧通向宾馆前台的木门。老邢等几个人拉着门上的铜把手,连拉带踹,门很快就被踹坏。人开始水一样往外涌。<br />
<br />
　　几个人抱着证据、沾有血迹的白色床单、白绿条纹的垫褥跟着人群往外走。小强站在门口试图阻挡人群,走在前面的几个男子一下子冲开了他。七十多个被关押的人,除了三个残疾者,都跟了上来。<br />
<br />
　　走廊里有四五个工作人员试图劝阻。“你们的事情都还没有处理呢。”<br />
<br />
　　人群高喊:“我们的事情不要你们处理了!”<br />
<br />
　　出了狭小的宾馆大厅,就是院子,院子外就是广阔的城市。老板大喊,“你们如果去了,你的事以后就不处理了。”“我们今天就处理你们强奸人的事。”人群高声回答,涌向距离聚源宾馆不到500米的洋桥派出所。<br />
<br />
　　宾馆老板的呼喊还是分解了队伍,一些人被解决问题的说辞劝回；有一些则惧怕于宾馆里的生活而直接回老家了。等到最后到洋桥派出所时,人群已经稀薄了。8 月5日凌晨,在洋桥派出所,干警介绍说,刑警已经迅速介入此案。在派出所的密闭审讯室里,刑警对当事人的讯问持续到事发次日凌晨。目击者中多人被要求配合警方调查,而包括小强的弟弟在内的3名嫌犯已被控制。派出所干警说,受害人的家属已接到通知,即将赶往北京。<br />
<br />
　　而这个晚上,那些冲出宾馆的被关押者们,则打算在一个街道花园里,度过他们出逃后的第一个夜晚。<br />
<br />
　　(文中王云丽、张建秀为化名)(本报记者丁补之亦有贡献)</div>

							
							
							
							
