													<h2>中国取得量子态隐形传输技术突破 超时空穿越或实现</h2>
						
						
																											<div id="postmessage_659686" class="t_msgfont">2010年06月04日 18:21:00 　<br />
<br />
&nbsp; &nbsp; 我国科学家首次实现远距离自由空间量子态隐形传输 为全球化量子通信奠定基础<br />
<br />
　　新华网合肥６月４日电（记者熊润频）存放着机密文件的保险箱被放入一个特殊装置之后，可以突然消失，并且同一瞬间出现在相距遥远的另一个特定装置中，被人方便地取出。记者从中国科学技术大学获悉，日前，由中国科大和清华大学组成的联合小组在量子态隐形传输技术上取得的新突破，可能使这种以往只能出现在科幻电影中的“超时空穿越”神奇场景变为现实。<br />
<br />
　　据联合小组研究成员彭承志教授介绍，作为未来量子通信网络的核心要素，量子态隐形传输是一种全新的通信方式，它传输的不再是经典信息，而是量子态携带的量子信息。<br />
<br />
　　“在经典状态下，一个个独立的光子各自携带信息，通过发送和接收装置进行信息传递。但是在量子状态下，两个纠缠的光子互为一组，互相关联，并且可以在一个地方神秘消失，不需要任何载体的携带，又在另一个地方瞬间神秘出现。量子态隐形传输利用的就是量子的这种特性，我们首先把一对携带着信息的纠缠的光子进行拆分，将其中一个光子发送到特定位置，这时，两地之间只需要知道其中一个光子的即时状态，就能准确推测另外一个光子的状态，从而实现类似‘超时空穿越’的通信方式。”彭承志说。<br />
<br />
　据介绍，量子态隐形传输一直是学术界和公众的关注焦点。１９９７年，奥地利蔡林格小组在室内首次完成了量子态隐形传输的原理性实验验证。２００４年，该小组利用多瑙河底的光纤信道，成功地将量子“超时空穿越”距离提高到６００米。但由于光纤信道中的损耗和环境的干扰，量子态隐形传输的距离难以大幅度提高。<br />
<br />
　　２００４年，中国科大潘建伟、彭承志等研究人员开始探索在自由空间实现更远距离的量子通信。在自由空间，环境对光量子态的干扰效应极小，而光子一旦穿透大气层进入外层空间，其损耗更是接近于零，这使得自由空间信道比光纤信道在远距离传输方面更具优势。<br />
<br />
　　据悉，该小组早在２００５年就在合肥创造了１３公里的自由空间双向量子纠缠“拆分”、发送的世界纪录，同时验证了在外层空间与地球之间分发纠缠光子的可行性。２００７年开始，中国科大――清华大学联合研究小组在北京架设了长达１６公里的自由空间量子信道，并取得了一系列关键技术突破，最终在２００９年成功实现了世界上最远距离的量子态隐形传输，证实了量子态隐形传输穿越大气层的可行性，为未来基于卫星中继的全球化量子通信网奠定了可靠基础。<br />
<br />
　　据悉，该成果已经发表在６月１日出版的英国《自然》杂志子刊《自然&#8226;光子学》上，并引起了国际学术界的广泛关注。</div>

							
							
							
							
