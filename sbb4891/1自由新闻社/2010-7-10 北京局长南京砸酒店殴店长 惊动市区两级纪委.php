													<h2>北京局长南京砸酒店殴店长 惊动市区两级纪委</h2>
						
						
																											<div id="postmessage_731361" class="t_msgfont">北京局长南京果盘砸店员称：南方人和北方人理解的扔不一样<br />
<br />
7月1日晚，在南京市珠江路咸亨酒店，北京市原崇文区卫生局副局长林钢一行消费时，因费用问题发生冲突。店方称林局长酒后动粗，手持水果盘砸到收银员头上，并用脚踹到店长胸口。调解后，林局长一方立下协议称赔偿2万元并道歉，但删除视频后立即变卦。昨天，林局长否认砸店员和踹店长一事，称协议是被逼迫立下的，属于无效协议。<br />
“林局”被指持盘砸店员<br />
南京市珠江路咸亨酒店的店长李女士在电话里告诉记者，7月1日晚上，一个从北京来的旅行团前往酒店消费，“一行8人中有3个导游”。就餐时，其中一名中年男子多次被别人用“林局”称呼。晚8点结账时，由于衔接工作上的失误，收银员将两盘赠送的菜也列入账单。随后，对方提出意见。得知情况后，收银员向对方退还多收的47元钱，“我也上前道歉，还给他们免去了76元的酒钱。”<br />
“但他们还是不依不饶”，李女士称，那名被人称作“林局”的中年男子自称是卫生局的领导，专门管餐饮的，多次要求店方给出账单并拍照，要取走证据后去告酒店。随后双方陷入僵持并发生口角。<br />
李女士称，就在大家不注意的时候，“林局”走向隔壁吧台，并拿来一盘装有西瓜、番茄的玻璃果盘，直接朝收银员砸去，“收银员躲了一下，但没躲开。”李女士表示当她上前阻止时，立刻被对方一脚踹到胸口。<br />
手机录像记录砸人现场<br />
店长李女士的手机录像画面显示，双方在收银台发生争执后，几名女子在收银台多次试图推倒收银台的电脑，一名男子则迅速转身离去，随后该男子手里拿着东西返回，并朝收银台砸去，将收银员的头部砸中。<br />
据随行人员中的当地导游田先生证实，当时砸人者就是“林局”，手里拿的是果盘。发生冲突后，有不少人去拉“林局”，由于收银台前围了不少人，他没有看到“林局”用脚踢李女士，只看到李女士倒地后很痛苦的样子。<br />
林局长称被迫签订协议<br />
据李女士称，“打人的‘林局’当晚没有去派出所，一直是随行人员在处理”，次日凌晨双方达成协议。<br />
记者在协议上看到，肖女士称受林先生委托处理酒店冲突一事，他们愿意赔偿店方2万元，林先生正式向店方赔礼道歉，店方需删除视听资料，双方不得向第三方泄露。<br />
李女士称，7月2日内容一删除，对方就立马变卦，拒绝赔付2万元。<br />
昨天晚上，记者在原崇文区卫生局见到副局长林钢。他承认此前到咸亨酒店消费，结账时与店方发生冲突，但否认打人，并表示协议是在对方胁迫下签署的。<br />
随行的刘科长在接受江苏电视台《零距离》采访时称，“在吃饭的时候，他们多收钱了，我们就说你们为什么多收我们钱，他们的领班态度特别特别不好”，“他是多喝了点，把盘子扔出去了，没有砸到人”。<br />
■对话林局长<br />
“南方人和北方人理解的扔不一样”<br />
记者：当时你和店方为什么发生冲突？<br />
林局长：我拿出消费的小票，要求他们给个说法，但他们态度很不好，当时就把小票涂乱了，虽然最后退钱了，但他们的消极服务态度太差。<br />
记者：店方说你拿着果盘砸了收银员，还用脚踹了店长？<br />
林局长：水果盘是塑料的，当时水果盘放在桌上，碰到地上摔坏了，掉的过程碰到电脑，我没拿水果盘砸收银员，也没有用脚踹店长。<br />
记者：手机视频里是你吗？林局长：视频里的人的确是我，但我那不是扔。<strong>南方人和北方人理解的扔不一样。</strong>当时我们喝的酒不多，我也根本没有喝醉。<br />
记者：店方说冲突发生后你从后门离开，晚上也没有去派出所？<br />
林局长：那位姓李的店长带着几十人把我们团团围住。我的血压高，就从后门离开酒店。我走之后酒店的情况就不了解了。当天晚上因为感到不舒服，我去医院做血压和心电图的检查，本来也是想回去解决问题，但医院说需要观察，第二天我才去酒店，也向他们道歉了。<br />
记者：你们在派出所立的协议是怎么回事？<br />
林局长：协议是无效的，因为签协议的人不是我，也不是随行的刘科长和李科长，而是一个小导游。导游是个小女孩，被酒店一方几十个人团团围住强迫签的。酒店那边说我是国家干部，至少要2万。只有重大伤害才会这么多钱，他们是在敲诈。<br />
记者：你们一起为什么有3个导游，是去南京旅游？<br />
林局长：我们之前是去上海市动物卫生监督所学习卫生管理经验，想去了解学习动物卫生监管模式。然后到了杭州，又到了南京，都是去学习。导游是因为我们对当地不熟，需要有人解决住宿问题。<br />
记者：你什么时候回到北京？酒店方说之后再也联系不上你？<br />
林局长：3日早上回到北京，回来之后经常接到威胁的电话，说要给曝光什么的，打电话根本不能确定身份，我就没有给对方答复。<br />
记者：现在如何解决？林局长：如果对方身体受到伤害，我可以道歉，但赔偿2万我不能接受，这是在敲诈。如果对方一直不讲理，我会通过法律手段解决这件事。他们是想借媒体炒作酒店。我不想炒作自己，这件事惊动了市纪委和区纪委，他们会向我了解情况的。<br />
