<br />
中新网11月19日电 (张哉麟)“朋友可以挑选，邻国不可以挑，有什么可以吵，有什么可以打？”成龙大哥今日在北京大发感慨<br />
<br />
<img src="http://www.chinanews.com.cn/yl/yl-yrfc/news/2009/11-20/U25P4T8D1974710F116DT20091120082041.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　原本是成龙的电影《大兵小将》同名网游启动的发布会，俨然成为大哥宣传慈善与和平的大舞台。在将近20分钟的个人访谈中，他滔滔不绝从前几天在新加坡参加为唇腭裂孩子做手术的活动，到如今帮一些大企业做平台，收取他们的善款为内地贫困地区捐款，越讲越起劲，丝毫不顾及主持人几次把话题拉回到网游上来。<br />
<br />
　　他笑着说，“我为大企业出席一个活动，他们给我钱，我就去做善事，这不是很好吗？”话锋一转，成龙笑着说，“只是大品牌哦，小品牌我不去，小品牌我要看他们给我多少钱。”语毕，台下传出阵阵笑声。<br />
<br />
　　说着说着，他开始透露自己明年4月将开拍一部新片，名为《十二生肖》。内容关于中国的国宝如何流出去，又如何拿回来，类似“寻宝”的故事。成龙坦言，灵感取自近期国外拍卖中国兽首的事。<br />
<br />
成龙继而讲到国与国、地区与地区之间的关系，“人们说，朋友你可以挑，但邻国你不可以挑，都存在几千年，几万年了，有什么可吵，有什么可打？一打经济就滑落，人民就遭殃，最重要是的，恢复起来需要好多年。”<br />
<br />
　　“有些国家为了三尺的地盘，就让几十万的人民受难，有什么用？”他越说越激动，“国家与国家之间要什么界限，地球属于全人类，世界需要和平、环保。”<br />
<br />
　　成龙也讲到自己年轻时拍《醉拳》是错的，“一直打打打，没有剧情的喜剧，打了十五分钟还是打。“他坦言，当初若读过书，就不会拍。但现在自己要把错误的东西纠正过来，希望拍一些有意义的东西。<br />
<br />
