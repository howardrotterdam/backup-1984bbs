													<h2>今日，皇上及诸大臣辛苦了</h2>
						
						
																											<div id="postmessage_68886" class="t_msgfont">4月5日，胡锦涛等领导人来到北京永定河森林公园参加首都义务植树活动。这是胡锦涛同少先队员一起给刚栽下的银杏树浇水。(新华社) <br />
　　4月5日上午，党和国家领导人胡锦涛、吴邦国、温家宝、贾庆林、习近平、李克强、贺国强、周永康等前往北京永定河森林公园，同首都干部群众和少先队员代表一起参加义务植树活动。 <br />
<br />
　　永定河森林公园位于北京市门头沟区，过去这里由于长期采挖沙石，形成了数十米深的大沙坑，成为北京西部最大的沙尘污染源。近年来，北京市不断加大永定沙坑地带的生态环境治理力度，将这一带规划为总面积3500亩的森林公园。 <br />
<br />
　　新华社报道，上午10时许，胡锦涛等中央领导同志陆续来到森林公园。一到植树点，胡锦涛就兴致勃勃地拿起铁锹，同首都科技工作者代表和北京市、国家林业局的负责同志一起铲土种树，先后种了一棵白皮松、一棵紫叶李和一棵银杏。每种下一棵树，总书记都微笑着招呼身边的少先队员提桶给树浇水。植树间隙，胡锦涛同参加植树的中国农业大学李宁教授聊了起来，详细了解我国生物育种研究的现状，勉励科技工作者集中力量，确保重点，争取在这方面有所突破。胡锦涛着重向北京市负责同志询问了北京绿化美化工作情况，叮嘱他们要切实把“绿色奥运”成果巩固好、发展好，真正让广大人民群众拥有一个优美宜人的生活环境。 <br />
<br />
　　植树现场，一片热火朝天的劳动景象。中央领导同志同在场的干部群众和少先队员代表一起，将一棵棵树苗植入刚挖好的树坑，然后铲土填坑、挥锹培土、提水浇灌，每道工序都做得十分认真。领导同志一边植树，一边同植树的干部群众和少先队员代表亲切交谈，询问他们的工作、学习和生活情况，现场不时传出阵阵欢声笑语。经过一番紧张劳动，一排排新栽下的树木昂然挺立，在风和日丽的明媚春光中展现着蓬勃生机。 <br />
<br />
　　植树过程中，胡锦涛对大家说，深入开展义务植树活动，不断扩大国土绿化面积，是实现科学发展的必然要求，也是建设生态文明的重要举措。希望全社会大力弘扬植绿、护绿、爱绿的文明新风，积极参与义务植树活动，注重科学、提高质量、加强管护，确保种一棵、活一棵、成材一棵。要通过一代又一代人的不懈努力，使祖国大地变得更加秀美。 <br />
<br />
　　参加义务植树活动的领导同志还有：王刚、王兆国、王岐山、回良玉、刘淇、刘云山、刘延东、张德江、徐才厚、郭伯雄、何勇、令计划、梁光烈、马凯、孟建柱等。中央和国家机关有关部门以及北京市的负责同志也参加了植树活动。 <br />
<br />
　　4月4日是第25个首都全民义务植树日。连日来，北京市近180万干部群众前往各个植树点，用自己的劳动为首都再添新绿。 <br />
