<br />
&nbsp; &nbsp;&nbsp; &nbsp;正在日本访问的世界维吾尔代表大会主席热比娅在东京单独会见本报记者表示，尽管中国政府策动两百多名中国留学京都的学生大闹她的演讲会场，但是她也收到一封汉族留学生写给她的信函表示，「我想在这做为一个汉族人向维吾尔人道歉，汉族人在中共谎言下做的伤害维吾尔人的事情是天理不容的，实在对不起」。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;热比娅在下榻的饭店将这名汉族留学生的信复印本交给本报记者指出，二十七日她在京都龙谷大学时，有两百多人手拿中国五星旗，大喊「热比娅是恐怖份子，Get out」，这些人只有少数是龙谷大学的学生，大多数是外面来的社会人，在现场有日本学生高举东突的旗子也被抢走。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;热比娅说，「我对这些学生的立场很同情，因为他们都是被迫来现场示威的」。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 她说，演讲之后，龙谷大学的校长交给她一封自称是「龙谷大学中国留学生」写给她的信，表示支持她，大意是「很高兴您能压住邪恶中共的压力…很多中国人从小接受中共的洗脑教育，在不明真相前提下，无知的抗议西藏人、抗议维吾尔人，以及中国其它少数民族，…我很清楚藏人和维吾尔人所受的伤害，在愤怒中共的同时，更加愤怒台湾屈服中共的压力，不惜牺牲自己的民主精神来指责您是恐怖份子，…您给家中的一个电话就把您说成恐怖主义份子和骚动策划者，足以见中共的无耻和台湾马英九政策的无知…做为汉族人的我永远支持您，我知道什么是正义什么是邪恶…」。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;热比娅是于二十二日在鹿儿岛国际大学、二十三日在鹿儿岛大学、鹿儿岛日治屋町教会、二十五日在自由大学、二十七日在龙谷大学、二十八日在京都某大学举行演讲，每场听众少者七百人，多者一千人，热比娅说，她在每场的演讲都强调，维吾尔人本来是很善良的，因为中国在自治区「每次把白的说成黑的」，而让维族感到失望，渐渐远离中国。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 热比娅也针对她访问台湾的可能性表示，现在无法奉告，「但是方向性很好」。</div>

							
							
							
							
