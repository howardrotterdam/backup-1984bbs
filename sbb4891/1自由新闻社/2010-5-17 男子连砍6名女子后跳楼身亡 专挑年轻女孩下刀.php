																											<div id="postmessage_619703" class="t_msgfont">男子连砍6名女子后跳楼身亡 专挑年轻女孩下刀<br />
 来源：大洋网-广州日报 2010年05月17日<br />
[提要] 16日晚上7时左右，在佛山南海区官窑厚西市场，一名年轻男子突然冲进人群挥刀狂砍，连砍6名女子后，在众人惊恐的目光中，从一幢3层小楼纵身跃下，当场死亡。据透露，目前入院的5人中，有2人重伤。对行凶男子的身份和作案动机，警方正在调查中<br />
　　昨晚事发佛山南海官窑厚村市场 5人入院其中2人重伤<br />
<br />
　　本报佛山讯 （记者张涨） 这本来只是一个平淡无奇的夏夜，但此刻，凉爽的晚风却混杂着咸腻的血腥味扑面而来，即便是回忆自己刚刚经历过的场面，也有人害怕地颤抖起来。<br />
<br />
　　这里是佛山市南海区官窑厚村市场。昨晚7时许，一名白衣年轻男子从贯穿市场的凤源西路一路挥舞菜刀，“专挑年轻女孩儿砍”，先后6人被砍伤送往医院，其中两人重伤尚在抢救之中。而行凶男子则在众人惊恐的目光中，从一幢3层小楼纵身跃下，当场死亡。<br />
<br />
　　现场目击：<br />
<br />
　　白衣男血洗一条街<br />
<br />
　　昨晚7时许，像往常一样，刚刚入夜时的厚村市场人声鼎沸，周围的街坊在市场中挑选肉菜好回家做饭。厚村市场被一条名为凤源西路的小路贯穿，宽不过5米。这条路刚刚进入市场的位置，有一家湘菜馆，血案从这里开始。<br />
<br />
　　“当时我只出去两分钟，去市场里买菜，回来就听店员说，店里一个女服务员被砍了。”在匆忙赶回店里的路上，店老板称或许与行凶者刚刚擦肩而过。“是个年轻男人，20出头，短发，穿一件浅色上衣，深色裤子。”一名店员回忆当时情况称，该男子右手举着一把菜刀，先是进入饭店2楼，在下楼过程中遇上一名年轻的女服务员，“一刀就砍下去，砍在左肩上。”鲜血在窄窄的楼梯上满目皆是。店里的人们还来不及反应，白衣男子举刀跑出，无人敢追上前去。慌忙中，有人马上报了警。<br />
<br />
　　随后，距离湘菜馆不远的一家士多里，又一名女孩被砍伤，“同样是砍在左肩上。”有目击者称，在整个过程中，行凶者都只下一刀，“见到女孩就砍，特别是年轻的。”顺着灯光昏暗的凤源西路，白衣男子走入厚村市场。<br />
<br />
　　小道交会处有彩票店、小超市以及一个药店。3名店主都说自己看到了当时神色慌张的白衣男子，“他把刀举过头顶，先是路过我的店，随后又折回来，进入店里。”彩票店老板称，白衣男正要从店里的楼梯上楼，被他制止，“我大声吼他&quot;你是干什么的！&quot;他不回答，被我推出门去。”随后，因女伴被砍伤而一路随行的男子闯入小超市，哭着央求老板“你一定要救救我！”<br />
<br />
　　此时，白衣男子却悄无声息地拐入彩票店后方，走上3楼，“听到扑通一声，像是有东西掉下来，我出门一看，一个人竟然躺在地上。”街后的一名店主告诉记者，当时并未注意到其随身携带的菜刀是否在身边。经了解，白衣男子当场死亡。<br />
<br />
　　此时已经接近晚上8时，市场内人潮越来越拥挤。随后，救护车、警车纷纷赶到，几个出事现场先后被拉上警戒线。<br />
<br />
　　惊人举动：<br />
<br />
　　白衣男三楼坠下身亡<br />
<br />
　　在市场附近的官窑医院，记者见到了受伤的5名女孩，另有一人因伤势较轻，包扎后已经返回家中。据医护人员介绍，5楼与6楼的病房中，5名伤者的伤痕都位于颈部、肩部和胸口位置，“基本每个人都只中了一刀。”<br />
<br />
　　据透露，目前入院的5人中，有2人重伤，至昨晚11时记者发稿时仍在抢救之中，另外3人伤情则较为稳定。<br />
<br />
　　市场内的档主都表示，此前从未见过行凶的白衣男子，“看他的样子也不像精神有问题，穿得很整洁，除了不说话外，没什么不正常的。”目击者称，所有被砍的女孩都没有与行凶者对话，“看起来双方都不认识”。<br />
<br />
　　目前，佛山警方已经介入该案，正在进行调查。<br />
<br />
　　医院探访：<br />
<br />
　　5人入院两人重伤<br />
<br />
　　受害者――郝亚楠，卤味档主，十八九岁<br />
<br />
　　卤肉档对面的一名老板口述：白衣男看到郝，进入档口又是一刀，砍在她的脖子上。郝亚楠当场倒地不起，随后男子镇定走出，继续前往市场深处。<br />
<br />
　　受害者――袁丽君<br />
<br />
　　其老公口述：我们正在市场几个档口挑菜，老婆一抬头，右肩当即中刀，正要扶起她之时，白衣男子又盯上下一个目标。<br />
<br />
　　受害者――卖食用油的女子<br />
<br />
　　路人口述：白衣男行凶后刚走出5米外，又砍伤了一名卖食用油的女子。当时他高举菜刀，口中不断呼喊着什么，没人听得清。</div>

							
							
							
							
