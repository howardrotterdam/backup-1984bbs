													<h2>20岁女兵遭吉林省军区高官奸污再被灭口</h2>
						
						
																											<div id="postmessage_349563" class="t_msgfont"><a href="http://dzh.mop.com/topic/main/readSubMain_10403911_0.html" target="_blank">http://dzh.mop.com/topic/main/readSubMain_10403911_0.html</a><br />
吉林长春市民陈大山的女儿陈啸，19岁在吉林通化师范学院上大学一年级时，适逢吉林省搞「阳光工程」招女兵，她「幸运」的M入省军区当通讯兵，不料在部队遭到多名军区高官L J，后惨遭杀害。家长得到的却是「心脏猝死」的死亡结论，但死亡证明书至今不给陈家。陈的妻子受女儿死亡刺激精神失常了。陈大山几年来得不到任何说法，逐级上访，饱受摧残。他说自己的家被C.P迫害得家破人亡，生不如死。<br />
<br />
陈大山表示，陈啸案曾被华尔街日报披露在海外网站，jt.h从而知晓了该案。吉林省go-vern-ment秘书长公开讲过，jt.h曾批示此案并派中央军委调查组去调查，但调查组徇私枉法、隐瞒包庇得出的结论令陈啸冤案再次石沉大海。陈大山决定向媒体曝光C.P军队的黑暗和那些高官们的丑恶嘴脸--「这些披着人皮的狼，都干了些什么事？」<br />
<br />
「阳光工程」 免交16万当兵<br />
<br />
近日陈大山在接受记者采访时讲述，近年在大陆东北地区，女孩要想入伍当兵得拖关系找门路至少得花费16万元才能参军的，这在部队已经司空见惯了，不是新闻。「入party多少钱、提干多少钱，都像商品一样明码实价的。」<br />
<br />
2005年，吉林省军区搞「阳光工程」，到大学招兵，凭学生实力挑选优秀大学生到部队。陈啸当时刚上大学半年时间，凭自己的实力没有花家里的一分钱，在2005年12月1日应徵入伍，成为吉林省军区通讯站话务排的战士。<br />
<br />
据陈大山了解，这批「阳光工程」招了10名大学生，其中5名M入吉林省军区，5名M入沈阳军区。<br />
<br />
当兵半年死亡<br />
<br />
陈啸M入部队当话务员仅半年就出事了。2006年6月27日，陈大山及家人被通知其女儿在部队病了。他与老伴及岳父一起赶到女儿服役的部队，看到的是在吉林省军区大院一个马路上，当官的让当兵的把陈啸往急救车上抬。急救中心的大夫告诉陈大山说，他们来的时候患者躺在马路上的，呼吸已经停止了、瞳孔已经扩散。因为奉行人道主义，所以走过场给她打了一针。<br />
<br />
陈大山说：「已死亡的女儿在救护车里。当天傍晚他们一直不让我们M救护车看我女儿。将近晚上 8点，我们家人才被允许上救护车看一眼女儿，那时部队的人拿来军装和衬衣衬裤，用酒精给陈啸清洁了一下，120的护士帮助给女儿换上了衣服。在换衣服过程中没发现身上有任何伤口。当时女儿的手指甲都是黑色的。军医和120急救中心给我的解释是：心脏停止跳动后，血液不流通了造成这种现象。当时我们一点也没往不好的地方想。」<br />
<br />
「陈啸的遗体被送到长春殡仪馆时已经是半夜12点了。转天等部队的人来处理这个事，一直等到下午5点才来人，谈话时对方上纲上线：陈啸是部队的人，要按照部队现役军人的规定来处理。我老岳父是C.P老八路，2006年已85岁高龄。他们就开始做他的工作。我岳父就对一大家子人说，要相信部队，相信解放军。家里人不想违背老人的意愿，对部队就信以为真吧。同意把孩子火化了。」当时吉林省军区领导口头承诺，除按国家抚血金之外，给家属的赔偿金一定让家属满意。<br />
<br />
妻子疯了 至今未得抚恤金<br />
<br />
陈大山刚刚遭遇了丧女之痛，就在女儿火化的当晚，他的妻子精神崩溃疯了。被强行送到吉林省吉大医院精神科，又转至「解放军208医院」。住M这家医院后竟发现，那里住着近百名20多岁左右的士兵，经打听得知，他们都是在部队被打疯的。<br />
<br />
「我没有生活来源，我妻子只有800元的退休金，连她吃药的钱都不够，她还需要人护理。只能靠老岳父的救济。体会到go-vern-ment没有人性，对我们从精神上M行迫害，逼得我们看不起病。到现在我们没有得到任何赔偿，连抚恤金都没有得到。」<br />
<br />
陈大山表示，2006年6月28日上午，沈阳军区后勤部工伤保险处处级干事吕晓明对他说，没想到吉林省军区这些人这么没有人性。他给我看2006年7月13 日省军区财务处给的单子。就是说沈阳军区批复了陈啸的抚恤金，但被吉林省军区扣押。我后来到省军区去找通讯站的营长，他对我撒谎隐瞒，最后也没给我个说法。从这之后我开始对女儿的死因产生怀疑！<br />
「猝死」无死亡证明<br />
<br />
陈大山至今仍未得到女儿的死亡证明书，女儿的死因也是一个迷。他说，孩子死亡不到24小时，120急救中心给开了假死亡证明，开死亡证明的人没有出具现场，没有任何证据，非常笼统。死亡证明上只有两个字：猝死。女儿的尸体匆忙火化后没有证据了。「是脑猝死？心脏猝死？还是什么猝死？公共安全专家局应该先出面认定死亡原因，之后家属到相关部门去取死亡证明。」<br />
<br />
「在殡仪馆和派出所，死亡证明书都要存档的，在我女儿死亡3个月后，我到当地派出所给女儿销户口，但办不成，因为吉林省军区不给我死亡证明，所以户口销不了。我找军队要死亡证明，军队的2个营级干部陪着我到派出所，他们拿着死亡证明，交给派出所，办理完手续后他们又把死亡证明拿走，就是不给我。我问凭啥不给我？他们说这是我们部队的，你女儿死后火化这些费用都是部队花的，死亡证明就得归我们。我没办法，我得不到。」<br />
<br />
陈大山还揭露，他女儿的病历也是伪造的。「假病历是7月17日写的，也就是在我女儿死亡20天之后写的，就与原始病历时间不符了，所以他们又把7月17日改回到死亡日期--6月27日。我们手里都有这个证据。始终在作假，漏洞百出。」<br />
<br />
他到军区去问孩子是怎么死的。陈啸的病历上写着心脏猝死，但是后面打了一个问号。「我根据病历找你们，为什么后面打个问号？就是说没有肯定是心脏猝死，初步诊断的结果是心脏猝死？」<br />
<br />
「我告120急救中心伪造病历和死亡证明，信访局不管，卫生厅不管，卫生部也不管。我找总政和沈阳军区做监定，到底军区做的病历是不是假的，他们到现在不敢接我的东西，都在拖我、推我。」<br />
<br />
知情军官：陈啸被多名军区高官L J致死<br />
<br />
陈大山对记者表示，女儿死后一年，突然有一天，一位不愿透露姓名的吉林省军区的中校军官来找他，他说良心不忍，对他透露了实情--是吉林省军区多名领导奸污了陈啸之后，再杀人灭口。<br />
陈大山：「2007年7、8月份，军区一名军官找我，我们见了面，他把这个事就说了。当时是孩子出事已经一年多了。他说，你老到部队去找，部队没有意愿给你处理。我知道这件事，当时看到了，现在在我心里压力很大，这件事始终折磨着我，我也是个父亲，我也有子女。作为家长要知道孩子的死因是正常的，但到现在部队不给你说法。」<br />
<br />
透露真相的军官对陈大山表示，我现在心里有一种犯罪感、心里非常压抑。如果不说出来，好像我和他们同流合污似的。我就把你孩子的死因和经过给你讲一下，你心里有个数，你怎么帮你的女儿去申冤，那就是你自己的事了，我帮不了你。而且我也不可能出面给你做什么证据。我今天给你把实话说了，我的良心可以放得开了，没有压力了，再往深一步我没办法做了。你能不能给女儿申冤，能不能得到国家的赔偿，那是你的事了。<br />
<br />
陈大山：「我当时听说孩子被多名领导祸害，我不相信，我想这个人给我讲这个到底什么意思，是不是与部队领导有什么不和之类的事？我当时对他还挺怀疑的。但是后来的3年多时间，在任何一个地方没有人敢告诉我孩子究竟怎么死的。」<br />
<br />
就即使如此，陈大山还始终相信，部队会查个水落石出的，所以关于女儿是被奸污致死这一点，无论是告状还是上访，他始终没有说出。只是说是被部队害死的。<br />
<br />
jt.h派中央军委调查组 无济于事<br />
<br />
陈大山被迫踏上上访之路。「我到吉林省军区上访，沈阳军区上访，他们不告诉我任何一个字，中央军委也不给我一个字的答覆，令我非常苦恼。他们不给我说法就证明我孩子的死因存在很大的疑点。再一点是说，我已经知道这个部队在造假了，和120急救中心互相在作假。」<br />
<br />
据吉林省go-vern-ment官员透露，后来jt.h知悉了此案，派中央军委调查组代表他下去调查案件。陈大山表示，吉林省军区仍然欺骗、作假；贪赃枉法。为了包庇吉林省军区领导的犯罪事实，中央军委调查组枉法给出陈啸是「心源性猝死」的荒唐结论。<br />
陈大山说，我到哪里告状都是一句话：军委调查组已经有结论了。而且到哪里告都没人管，现在人们心里都知道我女儿是怎么死的，就是没人敢说。我先后到沈阳军区和中央军委上访几十次，并给军委付主席徐才厚、gbx、总政部主任李继耐、总参谋部陈炳德总参谋长等军委首长和总政信访局洪局长，郝局长邮出上访反映信件几百封。时至今日在解放军三级信访单位（吉林省军区、沈阳军区、中央军委）仍未能讨到关于女儿枉死的任何说法。<br />
<br />
「我开始还怀疑透露内情的军官有什么动机、与领导之间有什么隔阂，而利用自己去报复对方。经过这些年上访申冤过程中看到的从上到下的包庇，我深切的感到如果自己的孩子不当兵，不出事，根本想像不到部队有多黑暗腐败，这位军官说的是良心话，是事实。」<br />
<br />
据陈大山分析，L J陈啸的部队官员都是军区的领导，得是校级以上军官，否则包庇的力度不会那么大。如果是些级别低的干部，不会中央军委都插手包庇。所以难度特别大，到沈阳军区找，干事说别来找我们，没有用，到中央军委去告吧，上访三年多了，现在没得到一个字的答覆。现在有的证据就是那份伪造的病历，对病历任何有权力的单位和领导都不敢给做监定。<br />
<br />
当局一次控制陈大山花费30万<br />
<br />
陈大山在3年的上访过程中饱尝心酸凌辱，找到哪里，对方都不讲理，不给他任何答覆。C.P16大前pol.ice驻守在陈大山家附近监控他们。他辗转到了北京又被50～60人抓回，羁押在宾馆里半个多月，每天4个pol.ice监视。后来一个长春市公共安全专家局的人对他透露，仅这一次对陈采取行动的花费是30多万。<br />
<br />
陈大山悲哀的表示，自己老年丧子，老婆疯了，他的精神压力非常大。「有可能我披露的这个事会导致生命危险，但是如果能为孩子申冤，我就不怕。」<br />
<br />
最后陈大山表示，2009年的徵兵工作又开始了，而且今年征女兵的数量比往年都大，我希望我女儿的悲剧不要在中国的军队中重演。不要再破坏老百姓的家庭。他还希望自己得到支持和同情，及C.P高层能对陈啸冤案引起重视。<br />
