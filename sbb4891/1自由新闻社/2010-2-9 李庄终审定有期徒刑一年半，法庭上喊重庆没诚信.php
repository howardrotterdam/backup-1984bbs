													<h2>李庄终审定有期徒刑一年半，法庭上喊重庆没诚信</h2>
						
						
																											<div id="postmessage_473651" class="t_msgfont">自曝有关部门承诺认罪就缓刑&nbsp;&nbsp;检察院做李庄工作让其换律师&nbsp;&nbsp;李庄案件全过程回放<br />
<br />
&nbsp; &nbsp; 今天上午，轰动国内的北京律师李庄被控伪造证据、妨害作证案在重庆市第一中级人民法院终审宣判，李庄被终审判决罪名成立，需入狱服刑一年六个月。<br />
<br />
&nbsp; &nbsp; 而就在今天上午宣判后，李庄的辩护人独家向本报披露，在二审开庭前，李庄在看守所内曾被某部门领导“谈话”。<br />
<br />
李庄罪名成立入狱一年六个月<br />
<br />
&nbsp; &nbsp; 今天上午9点30分，一脸轻松表情的李庄被法警带入审判大厅。从李庄的面部表情和神态来看，他对今天的判决结果充满信心。<br />
<br />
&nbsp; &nbsp; 宣判开始后，李庄听得非常认真，当他听到自己被判处有期徒刑一年半的时候，顿时跳了起来，乘法警不备，冲向话筒并一把抢在手里开始高喊：我在二审 的认罪是假的。是你们重庆有关部门在看守所承诺给我的，说只要我认罪就可以判缓刑。但是现在你们判了实体刑，你们不讲诚信。<br />
<br />
&nbsp; &nbsp; 据在现场的辩护人高子程介绍，当时现场场面一度失控，多名法警控制李庄才将其重新束缚。随后，记者采访了重庆法院的审判人员，据其介绍，之所以改判李庄一年六个月，主要原因是李庄在二审时当庭认罪，态度较好，符合减轻处罚的法定情节，所以经过二审法院改判为有期徒刑一年半。<br />
<br />
李庄称检察院找他做工作<br />
<br />
&nbsp; &nbsp; 李庄同时在法庭上还高声披露公诉人曾到看守所找他做工作，主要内容为三个方面。<br />
<br />
&nbsp; &nbsp; 第一，&nbsp; &nbsp;检察机关希望李庄二审认罪，态度好一些，不要给二审公诉机关难堪。<br />
<br />
&nbsp; &nbsp; 第二，检察机关希望李庄二审更换辩护人，不要再聘请高子程和陈有西为辩护人，以减少审判环节的麻烦。<br />
<br />
&nbsp; &nbsp; 第三是如果李庄不更换二审的辩护人，那么检察机关希望李庄可以做辩护人的工作，不要做无罪辩护，改为做罪轻辩护。<br />
<br />
&nbsp; &nbsp; 同时李庄还在法庭上高喊：“曾经有多少先烈写过认罪书，但是这不影响他们成为先烈……”<br />
<br />
&nbsp; &nbsp; “我希望全国16万名律师继续在外面为我伸冤……”<br />
<br />
&nbsp; &nbsp; 很快，情绪激动的李庄就被法警带离了审判大厅。<br />
<br />
辩护人披露李庄二审前“被谈话”<br />
<br />
&nbsp; &nbsp; 就在李庄案件宣判之后，记者第一时间电话采访了其辩护人，而其辩护人独家向本报披露了李庄案件的一个重要细节，也许这个细节可以解释为什么李庄在二审开庭时突然认罪。<br />
<br />
&nbsp; &nbsp; 据李庄的辩护人披露，在二审开庭之前，辩护人曾经于2009年12月31日和2010年1月15日两次去重庆看守所会见李庄。<br />
<br />
&nbsp; &nbsp; “那两次会见时，李庄的精神状态始终很好，并且高度亢奋地要求将官司打到底，要不惜一切代价，并声明哪怕是在监狱里服刑也绝对不认罪。但是当开庭时，李庄突然认罪，这使得我们几名辩护人都感到吃惊甚至是震惊。”辩护人说：“二审闭庭后，我到看守所会见李庄，问他为什么要认罪。李庄告诉我，在二审开庭前几天，有重庆市某部门的两人曾先后到看守所找他谈话，而谈话的内容李庄始终守口如瓶，并一再表示是秘密，绝对不能说。”<br />
<br />
&nbsp; &nbsp; 李庄的辩护人认为，这两个谈话对李庄的触动很大，同时也可能形成较大的压力，因此李庄才会在二审时撇开辩护人的无罪辩护当庭认罪。<br />
<br />
龚刚模死活尚无定数<br />
<br />
&nbsp; &nbsp; 在李庄案件二审宣判前，法律界早就有传言称如果李庄被定罪，那么龚刚模就一定可以逃过一死，理由就是他检举李庄属实，属于重大立功表现。<br />
<br />
&nbsp; &nbsp; 今天上午，当李庄被判决有罪后，记者立即采访了中国法学会刑法专业委员会委员张平，他表示尽管李庄被判有罪，但是龚刚模的死活现在仍尚无定数。<br />
<br />
&nbsp; &nbsp; “虽然我国刑法规定，被告人检举他人的犯罪一经查实，就属于有立功表现，但是李庄所犯的罪行尚不属于严重刑事犯罪，只是一般的刑事犯罪，因此龚刚模的立功行为也就不属于重大立功表现，其功能否抵得上其过还是一个未知数。”张平进一步解释说：“根据检察机关指控龚刚模的犯罪行为，恐怕不是一个简单的李庄案件就能够使龚刚模逃过一死的。也就是说，如果检察机关指控龚刚模的犯罪行为全部被法院采纳，那么恐怕他所犯下的罪行将会被同时判处两次死刑，这重严重的刑事犯罪又怎么能因为检举了一起普通的刑事案件而被减成死缓或其他刑罚呢！”<br />
<br />
&nbsp; &nbsp; 最后张平指出，如果仅仅因为龚刚模检举了李庄就逃过了一死，那么恐怕现在重庆打黑系列案件中的多名团伙主犯都会效仿，同时这也会给今后司法实践带来新的难题：“不杀一，不足以儆佰！”<br />
<br />
不足俩月走完全部程序被指罕见<br />
<br />
&nbsp; &nbsp; 从2009年12月12日李庄被警方采取强制措施到今天终审宣判，这一轰动国内乃至海外的案件全程历时仅仅57天，两两个月都不到，这就使得诸多法律界人士指责该案件“进展神速、国内罕见”。<br />
<br />
&nbsp; &nbsp; 首先，作为李庄案件的辩护人高子程就曾经在一审开庭时当庭指出该案件有违反刑事诉讼程序之嫌，使得辩护人连调查取证的时间都不够。但是对于辩护人的这一质疑，一审法院未给予任何的答复。<br />
<br />
&nbsp; &nbsp; 在李庄案件二审开庭后，多名法学界专家在北京就李庄案件召开了专题的专家论证会，论证会的结论是李庄案件违反刑事诉讼法所规定的多项时间限制，使得辩护人及相关的诉讼参加人根本没有时间开展工作，就被拖到了法庭上参加庭审。<br />
<br />
&nbsp; &nbsp; 一位与会的专家指出：“刑事诉讼法中规定了很多时间性的限制，例如几天内完成询问，几天到几天内完成强制措施的审批，几天到几天内完成审查起诉等。这些时间的限制及为了防止出现超期羁押规定了时间的上限，又为了防止出现辩护人及诉讼参加人准备时间不足而规定了下限。但是李庄案件从案发到终审连两个月都不到，简直就是国内罕见。这不能不说是一种抢时间抢进度的办案，省略了程序就一定会出现误差或失误，但是这种做法到现在为止尚无人提出质疑。”<br />
<br />
&nbsp; &nbsp; 有与会专家质疑，在李庄案件中，神速的进展使得一些办案单位根本没有时间来详细审查。例如我国检察机关的职责有两个，一是对案件本身进行审查起诉，另一个就是对案件的办案程序进行审查监督。但是李庄的案件是12月17日到的检察院，18日就被起诉到了法院，只在检察院呆了一夜，这一夜的时间哪怕是加班加点也是无法对案件程序进行合法性审查的，所以说在李庄案件中检察机关根本没时间去履行自己的检察监督职责。而同样的情况在李庄案件中还有很多。<br />
<br />
重庆高院回应：我们程序合法<br />
<br />
&nbsp; &nbsp; 针对这些质疑，日前重庆市高级人民法院院长在接受媒体采访时，曾作出了专门的回应。<br />
<br />
&nbsp; &nbsp; 重庆高院回应指出，追究某一个律师的刑事责任，绝不意味着要否定中国律师制度，绝不意味着要否定中国的诉讼制度，正如去年重庆法院几个法官出了问题，却不能因此就否定重庆法院的法官队伍，进而否定中国的法官队伍。<br />
<br />
&nbsp; &nbsp; 律师是推进中国民主法治进程的重要力量，同时也是重庆打黑除恶审判的重要参与者。在迄今为止的打黑除恶审判中，有将近500名律师参与其中，发挥了重要作用。不少律师的辩护意见被法院采纳。<br />
<br />
&nbsp; &nbsp; 关于李庄的案件，重庆的法院是严格按照程序法规定的时间来审理的，但是程序法并没有规定一个程序最短的时间，不过法院还是保障了辩护人阅卷和会见的权利的。<br />
<br />
罪名成立李庄律师资格将被注销<br />
<br />
&nbsp; &nbsp; 在获悉李庄终审被判有罪后，记者立即电话采访了北京市律师协会，就李庄的执业资格问题进行了采访。<br />
<br />
&nbsp; &nbsp; “依据我国《律师法》，注册律师一旦被因故意犯罪而被追究刑事责任，哪怕是被判处了缓刑，那么其律师资格就将被依法注销。”律师协会的工作人员介绍说： “李庄案件在一审宣判有罪后，他提起了上诉，使得一审的判决没有发生法律效力，所以我们在其一审被判有罪后并没有立即启动注销的程序。但是今天李庄终审被判有罪，这就等于是他的终审判决。我们律师协会将依据这一判决及《律师法》的规定，启动注销程序，最快李庄的律师资格将在春节放假上班后被注销。”<br />
<br />
&nbsp; &nbsp; 记者同时还了解到，关于注销律师资格的程序主要是由律师协会下属的纪律委员会首先开会研究，然后向律师协会通报研究结果。再由律师协会向律师管理机构也就是司法局的律师管理处呈报相关文件，最终由律师管理处报请司法部律师司下达注销的文件。<br />
<br />
李庄案件全过程回放<br />
<br />
李庄案到今天为止已经是尘埃落定，那么在这短短的57天时间里，该案件到底有哪些值得关注的截点呢，本报就此将该案件的全程进行回放，也算是为这一颇据争议的案件进行一次时间上的盘点。<br />
<br />
2009年11月中旬，重庆涉黑的某团伙主犯龚刚模家属来北京找到李庄，请求其为龚刚模进行辩护。在签定了《授权委托书》等一系列代理手续后，李庄成为龚刚模的辩护人，其助理马晓军与李庄一起动身前往重庆。<br />
<br />
2009年11月24日、26日及12月4日，李庄先后三次会见龚刚模。在会见中，李庄因警方始终派员在场，与警方人员产生语言冲突。<br />
<br />
2009年12月10日，龚刚模检举李庄，称李庄以挤眼睛的方式教唆了他编造“被刑讯逼供”的虚假口供。<br />
<br />
2009年12月11日，李庄被其所在的事务所紧急召回北京，并于当天向重庆法院方面书面通报，终止为龚刚模进行辩护。<br />
<br />
2009年2月12日，李庄在北京被重庆警方抓捕，当夜被押解回重庆，并于12月13日被刑事拘留，12月14日被逮捕。其所涉嫌的罪名是刑法第 306条规定的“辩护人伪造证据、妨害作证罪”。<br />
<br />
2009年12月13日，中国青年报、中央电视台等多家新闻媒体开始大篇幅披露李庄案情，并公布李庄所谓的短信“钱多、人傻、够黑、速来”，事后经过证实，该短信并非李庄所发。<br />
<br />
2009年12月17日，该案件被移送检察院审查起诉；12月18日，该案件就被检察院起诉到重庆市江北区人民法院。<br />
<br />
2009年12月20日，著名刑事辩护律师高子程、陈有西接受李庄家属委托，为李庄进行辩护。<br />
<br />
2009年12月28日，高子程提出为龚刚模进行伤痕坚定，以确定其是否有外伤。12月29日，法医鉴定出炉，经坚定龚刚模手部有明显伤痕。<br />
<br />
2009年12月30日上午9点到12月31日凌晨1点，在长达16个小时的庭审后，李庄案件一审休庭。<br />
<br />
2010年1月5日，龚刚模案件开庭审理，在该案件全部被告人都声称被刑讯逼供的前提下，首犯龚刚模对刑讯逼供只字不提。<br />
<br />
2010年1月8日，李庄被一审宣判有罪，入狱2年6个月。李庄不服，于判决生效前的1月18日正式上诉，坚称自己无罪。<br />
<br />
2010年1月15日，李庄的辩护人再次到看守所会见他，他依旧表示自己无罪，要将官司打到底。<br />
<br />
2010年2月3日，李庄案件二审开庭，令全国公众大跌眼镜的是，在二审中李庄当庭认罪。<br />
<br />
2010年2月4日，辩护人会见李庄，问其为什么认罪。李庄披露其“被谈话”。<br />
<br />
2010年2月9日，李庄终审被判有罪，刑期为一年六个月。</div>

							
							
							
							
