													<h2>李庄曝&quot;诉辩交易&quot; 指责有关部门失信</h2>
						
						
																											<div id="postmessage_473992" class="t_msgfont"><a href="http://news.163.com/10/0209/13/5V378MMF0001124J.html" target="_blank">http://news.163.com/10/0209/13/5V378MMF0001124J.html</a><br />
<br />
北京晚报2月9日报道 今天上午10时，重庆市一中法院对律师李庄伪造证据、妨害作证案进行二审宣判：维持江北区法院一审判决的定罪部分，即李庄犯辩护人伪造证据、妨害作证罪成立。撤销原判有期徒刑两年六个月的量刑部分，判处李庄有期徒刑一年六个月。也就是说，李庄庭审时的突然认罪为自己减少了一年铁窗生活。李庄显然认为判决没有达到预期，并当庭大曝“诉辩交易”，表示二审认罪有假，恳请16万中国律师为其申诉到底。<br />
<br />
一审定罪认定不变<br />
<br />
法院审理查明，2009年11月24日、26日、12月4日，李庄、马晓军先后在重庆市江北区看守所会见龚刚模。李庄在会见过程中，唆使龚刚模在法庭审理时谎称被公安机关刑讯逼供，向龚刚模宣读同案被告人樊奇杭的部分供述，以配合龚刚模推翻以前在公安机关的供述。李庄又指使重庆克雷特律师事务所律师吴家友贿买警察，以证明龚刚模被公安机关刑讯逼供的虚假事实。<br />
<br />
2009年11月底至12月初，李庄引诱龚刚模之妻程琪作龚刚模被樊奇杭等人敲诈的虚假证言，并要求程琪出庭作证。同年11月24日，李庄指使龚刚模之兄龚刚华安排重庆保利天源娱乐有限公司(下称保利公司)员工作伪证，否认龚刚模系保利公司的实际出资人和控制者。龚刚华遂授意保利公司员工向公安机关提供虚假证言。同年12月10日，龚刚模向公安机关检举李庄的行为。同月12日，李庄被公安机关捉获。审理中，上诉人李庄当庭认罪。<br />
<br />
从判决中可见，一审中认定的事实在二审判决中被全部认定。<br />
<br />
记者联系上李庄的辩护律师高子程，高律师说，二审对刑法306条仍然认为是行为犯，而非结果犯。法院认为李庄在担任龚刚模的辩护人期间，教唆龚刚模作被刑讯逼供的虚假供述，引诱、指使证人作伪证，指使他人贿买警察作伪证，其行为妨害了司法机关正常的诉讼秩序，已构成辩护人伪造证据、妨害作证罪。<br />
<br />
“比如马晓军的证言，指证李庄有教唆行为的部分法院就采用，而对他表示会见龚刚模的笔录都是真实的部分都没有采纳。也就是说，对公诉方有利的证据就采用，对公诉方不利的证据就不采用。”高子程无奈地表示，他们提出的公诉机关没有拿出李庄提供的伪造的证据，也没有妨害任何证人作证等无罪辩护意见未被采纳。<br />
<br />
李庄认罪获从轻处罚<br />
<br />
在二审开庭时，李庄在辩护人都毫不知情的情况下，承认了一审的全部指控。表示一审认定事实清楚，证据确实充分，适用法律适当，并撤回上诉理由。<br />
<br />
据记者了解，庭审后，辩护律师带着一肚子不解会见李庄，向其询问突然认罪的原因。“李庄除了说了句‘惊天秘密’，再没做任何解释。”<br />
<br />
在李庄认罪后，本报曾多方采访，猜测其认罪原因。被业内人士认为最有可能的就是李庄以认罪方式获得从轻处罚，改判缓刑。著名刑辩律师李肖霖就分析说，李庄认罪谋缓刑很可能竹篮打水一场空，因为他认罪就恰恰支持警方的观点，就是抓对了人。<br />
<br />
事实上，李庄的认罪在二审中确实起到了一定的作用。二审法院考虑其认罪态度，依法对其从轻处罚，撤消了原判两年六个月有期徒刑的量刑判决，判处李庄有期徒刑一年六个月。虽然减了一年刑期，但仍然是实刑。<br />
<br />
高子程律师对改判的结果表示：“只有从轻处罚才能显示李庄认罪的结果。但是改判只是在掩盖，是用从轻处罚来掩盖李庄无罪的事实。”<br />
<br />
李庄当庭大喊认罪有交易<br />
<br />
“在听到判决结果后，李庄情绪非常激动，气愤地对着话筒大喊。他说自己没有认罪，二审的认罪是假的!”在描述李庄庭上表现的过程中，高子程律师的声调都提高了八度。<br />
<br />
高子程律师说，昨天他去会见李庄时，李庄表示公诉人让他做辩护律师的工作，让他们做有罪辩护。李庄回复说：“我说服不了他们。”<br />
<br />
在整个案件过程中，无论是李庄喊冤，还是李庄认罪，高子程、陈有西两位律师坚持为其做无罪辩护。辩护词、申请书写了几万字。<br />
<br />
对于一年半的判决结果，李庄显然无法接受，他表示会申诉到底，甚至当庭大曝认罪的原因。“李庄当庭大喊，开庭前有关领导做过他的工作，承诺只要认罪就判缓刑。现在这个结果显然丧失诚信。开庭前，公诉人还让他撤回上诉。”高子程律师说：“李庄被带出法庭时，一边走一边说再见，恳请16万中国律师为其申诉。”<br />
<br />
高律师评价说，今天的宣判又是一场闹剧。对二审判决的决定者而言，这纸判决非常滑稽。“首先庭审过程滑稽。李庄二审时表示撤回上诉理由。我问李庄‘你撤回上诉吗?’李庄说坚持上诉。李庄既没有说新的上诉理由，也没有说上诉请求，庭审就往下进行了。第二，李庄在庭上自称一审事实清楚，证据确实充分，这实际上是种认罪态度，但并非真的认罪。他从没承认过实施什么行为。从李庄开庭的六点陈述来看，可以解读出他是被迫认罪。”<br />
<br />
<br />
李庄开庭时承认一审认定事实清楚，还写了悔过书，如今又说并非真的认罪，让人吃惊。高子程律师说：“李庄的表现昭示着他从未真正认罪。”<br />
<br />
名词解释：诉辩交易<br />
<br />
诉辩交易（Plea Bargaining），又称为诉辩谈判或者诉辩协议，是指在刑事诉讼中法院开庭审理之前，提起控诉的检察官为了换取被告作有罪答辩，提供比原来指控更轻的罪名指控或者减少控诉罪行，或者允诺向法院提出有利于被告人的量刑建议为条件，与被告方(一般通过律师)在法庭外进行协商谈判而形成的一种司法制度。（来源：中国法院网）</div>

							
							
							
							
