1983，郑州市上街区；男女谈恋爱，由于双方家庭条件悬殊过大，女方家长一直不同意婚事，无奈女方毅然决然和恋人半公开同居以此来要挟父母就犯，女方家长闻之大怒，叫来众多亲朋好友强行把女抢回并对男青年大打出手。谁知男青年对此女是一往情深，尽管受到如此待遇也对女方痴心一片，仍隔三差五‘骚扰’女方。女方家长终于下了毒心，把女儿关在家中洗脑一月有余，硬是诱逼着女儿告男方强奸，并拿出了之前准备好的‘证据’......赁着女方家的社会关系，不出一月，男青年命丧刑场。 <br />
