																											<div id="postmessage_92482" class="t_msgfont">83“严打”第一战役：死刑24000人<br />
<br />
作者：秦德良<br />
<br />
(作者单位:西南民族大学法学院)<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;“严打”是在中共党中央和政府统一领导下，由各级中共党组织和各级政府牵头，组织各级公、检、法部门集中时间，集中力量开展的一场全国性的严厉打击严重刑事犯罪的斗争。“严打”突出两个“严”字，一是打击重点是严重犯罪活动；一是对严重犯罪活动的打击要严厉，坚决贯彻依法从重从快的方针，稳、准、狠地打击犯罪分子。[1] 它包括实体和程序两方面：“从重”是就实体而言，“从快”是就程序而言。二者又分别体现为司法和立法两个层面：从司法层面看，“严打”是一种在现行刑法、刑事诉讼法范围内“从重”、“从快”的执法活动，这也是一般意义上的“严打”含义。[2] 但“严打”有时也体现在立法层面上，如1983年的“严打”，全国人大常委会于当年9月2日颁布实施了《关于严惩严重危害社会治安的犯罪分子的决定》和《关于迅速审判严重危害社会治安的犯罪分子的程序的决定》。[3]<br />
<br />
“严打”刑事政策在我国有悠久的历史传统。当代中国“严打”政策具有典型的中国特色，与西方国家“重重”的“严打”政策不同，一个最突出的特点是我国“严打”政策的典型的政治性、军事性、行政性和阶级专政性。“严打”刑事政策在我国20世纪50年代确立，80年代得到进一步发展。<br />
<br />
<br />
自20世纪70年代末期以来，我国一直实行依法从重从快打击严重经济犯罪和严重刑事犯罪的刑事政策。这段时期的从重从快的刑事政策，最早是在1979年针对转型社会初期的犯罪高峰提出。对“严打”贡献了思想的主要是彭真与邓小平。<br />
<br />
<br />
1、彭真的“严打”观点<br />
<br />
第一，“依法从重从快”<br />
<br />
1979年11月22日至26日，由彭真主持召开的全国城市治安会议，根据社会治安形势严峻的状况，首先提出了对极少数杀人、抢劫、强奸、放火、爆炸和其他严重破坏社会秩序的犯罪（“六类”案件）依法从重从快打击，实现社会治安综合治理的思路。<br />
<br />
1980年１月23日上午在听取上海公、检、法负责同志汇报了上海的治安问题后，彭真说“当前，对现行犯罪分子的处理，不能从轻，要从重；不能从慢，要从快” [17]<br />
<br />
1981年５月彭真同志亲自主持召开了北京、上海等５大城市治安座谈会。在这次座谈会上，他明确提出：“对恶性案件的现行犯，必须依法从重、从快处理，对他们的放纵、宽大就是对人民的残忍。”[18] “从重是在法律规定的量刑幅度以内的从重，不是加重；加重是在法律规定的量刑幅度以外加重判刑。”[19]<br />
<br />
第二，“要稳，要准”<br />
<br />
1981年５月，他明确提出： “要稳，要准，要严格地以事实为根据，以法律为难绳，最主要的是对犯罪的基本事实、判刑的根据要查清楚。”[20]<br />
<br />
第三，“两个基本”<br />
<br />
1980年2月，彭真在广东听取公检法三家的汇报时指出，对严重危害社会治安的刑事犯罪分子的处理，只要基本事实清楚，基本证据确凿，就要依法从重从快予以严厉打击。[21] <br />
<br />
这是彭真首次提出，后来成为指导司法机关办案的“两个基本”的原则。<br />
<br />
1981年５月，他明确提出：“一个案件，只要有确实的基本的证据，基本的情节清楚，就可以判，一个案件几桩罪行，只要主要罪行证据确凿也可以判”[22]<br />
<br />
第四，“死刑核准权下放”<br />
<br />
“为了及时处理杀人、强奸、抢劫、放火、爆炸等恶性现行刑事犯中的首要分子，对这类人的死刑核准权以下放给省、自治区、直辖市高级法院为好。”[23]<br />
<br />
第五、“严打就是人民民主专政”<br />
<br />
“在党的统一领导下，公、检、法三机关是分工负责的三道工序，互相协作、配合、互相制约。”[24] 1979年10月13日，彭真在《实现四化一定要有一个生动、活泼、安定团结的政治局面》的讲话中指出：“公、检、法机关是无产阶级专政的武器，是党和人民的刀把子，根本任务是打击敌人、保护人民。”[25] 1985年1月28日彭真在全国政法工作会议上的讲话指出“政法队伍是党的卫队，国家的卫队，人民的卫队”，[26] 1987年3月31日他在一篇文章中指出“政法部门是为人民服务、为社会主义服务的，是人民民主专政的一个重要工具。”[27]<br />
<br />
第六，“严打是长期的对敌人专政的斗争”<br />
<br />
“不能设想严打斗争可以一劳永逸不再需要进行。一切行之有效的专政手段和方法都不可以丢掉。至于怎么搞，我看是大来大搞，中来中搞，小来小搞。”[28]<br />
<br />
第七，“严打”是社会治安综合治理刑事政策的一个方面<br />
<br />
1981年５月，彭真亲自主持召开的北京、上海等５大城市治安座谈会。在针对当时社会治安的严重情况提出要依法从重从快惩处严重危害社会治安的犯罪分子的同时，又提出对大量有轻微违法犯罪行为的青少年，要加紧进行教育、感化和挽救工作，预防犯罪，实现社会治安综合治理，首次将“严打”与社会治安综合治理刑事政策结合了起来。<br />
<br />
<br />
2、邓小平的“严打”的观点<br />
<br />
首先，严打必须依法从重从快从严集中打击。<br />
<br />
1982年，小平针对经济犯罪指出“现在刹这个风，一定要从快从严从重”[29]，1983年，小平针对严重刑事犯罪活动指出“解决刑事犯罪的问题是长期的斗争，需要做各方面的工作。现在是非常状态，必须依法从重从快集中打击，严才能治住。搞得不痛不痒，不得人心。我们说加强人民民主专政，这就是人民民主专政。要讲人道主义，我们保护最大多数人的安全，这就是最大的人道主义！”[30]<br />
<br />
其次，严打必须发动群众、组织大战役和抓大案要案。<br />
<br />
1983年７月19日，小平和彭真谈话说：“刑事案件、恶性案件大幅度增长，这种情况很不得人心。”“主要是下不了手，对犯罪分子打击不严，不快，判得很轻。”“为什么不可以组织一次、二次、三次严厉打击刑事犯罪活动的战役？每个大中城市，都要在三年内组织几次战役”“我们说过不搞运动，但集中打击严惩刑事犯罪活动还必须发动群众。”[31] <br />
<br />
再次，严厉打击各种严重犯罪活动是长期的经常的斗争。邓小平说：“打击经济犯罪活动，我们说不搞运动，但我们一定要说．这是一个长期的经常的斗争。我看，至少是伴随到实现四个现代化那一天”。[32] <br />
<br />
最后，“严打”是新时期的阶级斗争的表现，是对少数教育不改悔的罪犯进行专政的斗争。<br />
邓小平指出，应该把严重刑事犯罪分子当作敌我矛盾来处理。在他看来，“严打”决策符合马克思列宁主义关于无产阶级专政学说的科学原理。“我们一直坚持对各种敌对势力、反革命分子、严重危害社会秩序的刑事犯罪分子实行专政，决不对他们心慈手软”。[33]<br />
<br />
彭真、邓小平的“严打”观点集中反映了中共在转型时期对解决严重经济犯罪问题与严重刑事犯罪问题的探索，从“严打”实践来看，这一探索在总体上基本上没有超出20世纪50年代毛泽东的镇反“严打”政策，但在理论上有一些突破，特别是将“严打”政策与社会治安综合治理刑事政策结合了起来，将严厉打击严重刑事犯罪作为社会治安综合治理的首要环节，这是镇反时期没有的。<br />
<br />
<br />
“严打”的大规模实践<br />
<br />
1982年1月11日《中共中央紧急通知》、1982年3月8日第五届全国人民代表大会常务委员会通过《全国人民代表大会常务委员会关于严惩严重破坏经济的罪犯的决定》、1982年4月13日《中共中央、国务院关于打击经济领域中严重犯罪活动的决定》公布，我国实际上开始了严厉打击严重经济犯罪的“严打”斗争。<br />
1983年8月全国政法工作会议召开、1983年8月25日中共中央发出《关于严厉打击刑事犯罪活动的决定》以及1983年9月2日全国人大常委会通过了《全国人民代表大会常务委员会关于严惩严重危害社会治安的犯罪分子的决定》和《全国人民代表大会常务委员会关于迅速审判严重危害社会治安的犯罪分子的程序的决定》，标志严厉打击社会治安领域犯罪活动的“严打”斗争正式启动。从此“严打”几乎在所有社会生活领域展开，中国因此进入以“严打”作为对社会进行法律控制和政治控制的重要手段时期。<br />
<br />
从1982年在经济领域开始的“严打”至今已近二十年，历经三个大阶段。在三阶段中除1985年外，每年都组织“严打”战役或者“严打”专项斗争。<br />
<br />
 <br />
<br />
1982年打击经济犯罪以及1983年8月至1987年1月全国性的“严打”三大战役<br />
<br />
从1982年1月至1983年4月底，“严打”经济犯罪的斗争已经如上所述。从1983年8月开始，我国开始了严厉打击社会治安领域犯罪活动的“严打”斗争，这次 “严打”将杀人、强奸、抢劫、爆炸、流氓、致人重伤或者死亡、拐卖人口、非法制造、买卖、运输或者盗窃、抢夺枪支、弹药、爆炸物、组织反动会道门、引诱、容留、强迫妇女卖淫、传授犯罪方法等危害社会治安的犯罪确定为打击重点。<br />
<br />
(1) 第一战役从1983年8月至1984年7月，在全国范围打了三仗，有的地方打了四仗。第一仗主要是打击浮在面上的犯罪分子，搜捕了一大批犯罪分子，破获了一批久侦未破的积案。第二仗是深挖犯罪，集中搜捕流窜犯、逃犯，打击流氓团伙，大力侦破大案、要案和重大积案。第三仗是巩固前两个战役的成果，挖出一批隐藏较深的大要案。<br />
<br />
“在第一战役中，全国公安机关共逮捕杀人、放火、抢劫、强奸、流氓等罪犯1027000人，检察机关起诉975000人，法院判处861000人，其中判死刑的24000人，司法行政部门接收劳改犯687000人，劳教人员169000人。这是1950年镇反运动以来规模最大的一次集中打击。”[35]<br />
<br />
(2) 第二战役从1984年8月到1985年12月，分四仗。主要是使“严打”斗争向纵深发展，打击那些最严重的犯罪分子，并广泛深入地发动群众，加强政治攻势，瓦解犯罪分子。 <br />
<br />
(3) 第三战役从1986年4月上旬至国庆节，分打击流氓、打击流窜和打击盗窃三仗。打击的主要对象还是最严重的犯罪分子，但是不搞全国统一行动，不统一规定时间，边打击，边防范；边打击，边建设，逐步过渡到正常。<br />
<br />
三大战役历时3年5个月，取得了巨大成果。全国治安状况有了明显好转，共查获各种犯罪团伙19.7万个，查处团伙成员87.6万人，全国共逮捕177.2万人，判刑174.7万人，劳动教养32.1万人。[36]<br />
<br />
 <br />
<br />
“严打”政策并未取得预期结果<br />
<br />
<br />
共和国初期的“严打”主要作用是维护和巩固了国家政权，这是最主要的成绩，但负面影响也很大。而从20世纪80年代开始以来几乎没有停止过的“严打”，尤其是1983，1996，2001年三次全国集中统一的大规模的政治化、军事化的“严打”斗争，并没有达到预期的结果。犯罪高峰一浪又一浪是这个政策尴尬处境的最好注释。虽然<font color="#FF7F50">“中国每年处决万名死囚”[[/COLOR]40] 但如此“严打”高压政策依然没有能够阻止日益发展的犯罪和社会治安恶化的趋势。根据《2001-2002年：中国社会形势分析与预测总报告》，即使在2001年“严打”期间，2001年的社会治安秩序没有根本性的好转。[41] 这充分说明“从重从快”的、具有中国特色的、把罪犯当成敌人的、实现人民民主专政的、具有一定阶级斗争形式的“严打”既不可能对所有的犯罪“一网打尽”，也不能从根本上制止犯罪的增加，甚至它对阻止犯罪过快增加所起的延缓作用也是极为有限的。<br />
<br />
<br />
综观20多年来严打整治斗争的历程，出现了这样一个年复一年的“怪圈”，即：发案，破案，抓人；发案多，破案多，抓人多；发案更多，破案更多，抓人更多。简而言之，就是“打不胜打、破不胜破”。[42] “严打”形成“打不胜打、防不胜防”“打疲了警察，打油了罪犯”的怪圈，的确值得我们深思。<br />
<br />
犯罪发展有一定规律，面对犯罪高峰，严厉打击严重刑事犯罪这可以说是任何一个法治国家都会实施的刑事政策。但中国的“严打”刑事政策与国外不同，国外的“严打”可以称为“重重”，即对严重刑事犯罪从重打击。而中国特色的“严打”有鲜明特点：由执政党与政府发动，全民动员，大造声势，把罪犯当作敌人，“严打”一定程度上变成了实现人民民主专政的政治斗争、阶级斗争和军事斗争。司法仅仅成了执政党与政府实施“严打”刑事政策的工具，这深深体现了20世纪镇反“严打”的烙印。在“严打”刑事政策的高压下探索法治治理模式，这本身就是一个矛盾。现代中国对法治的理想与不懈追求以及认真践履的态度，中国改革开放以来所面临的各种综合性、全局性的社会矛盾，解决社会矛盾、处理违法犯罪手段的单一，追求短期功利效应等因素促成了“严打”刑事政策处于欲罢不能、欲说还羞的尴尬状态。实施了多年的“严打”刑事政策已经使这一政策所能够发挥的优势作用发挥殆尽，其之所以尴尬，根本上因为这一政策本身与法治模式不合。</div>

							
							
							
							
