 <br />
<img src="http://pdnedu.blogs.com/.a/6a00d8341ce76f53ef0115713e4c08970b-800wi" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
On Saturday, a shocking video of a young woman bleeding to death appeared on social networking sites.<br />
<br />
Anonymous and impossible to trace, the clip went viral with a story attached: The woman is Neda, an opposition protester in Iran, who was gunned down by a government sniper Saturday on the streets of Tehran. We don't know how much of this is true. There is no way to verify even basic information about the video. But the clip proved too strong to be bogged down by fact-checking. The witnesses heard in the clip express shock, then desperation, then utter helplessness, passing through an emotional arc in 40 seconds. A viewer can't help but imagine being there, powerless to do anything other than keep the camera on.<br />
<br />
&quot;Neda&quot; quickly hopped from Facebook to YouTube to Twitter to blogs to mainstream media. Even &quot;The Today Show&quot; aired part of it this morning. A still from the video appears above the fold on the front of The Wall Street Journal today.<br />
<br />
Above, we've placed a frame grab from the video alongside Robert Capa's &quot;The Falling Soldier,&quot; the most famous moment-of-death image in photography. Are we overreaching in comparing the two? <br />
<br />
<br />
Consider the similarities. Like &quot;Neda,&quot; the 1936 image &quot;The Falling Soldier&quot; stands as a timeless symbol of war, even as scholars keep debating what it actually shows. In 1936 armed conflict was a soldier falling in a field. Today it's a civilian felled in an urban clash. <br />
<br />
Both images signify the emergence of a new type of wartime reporting. Today, a 40-second clip shot on a tiny camera or cell phone can go online in minutes, and be influencing worldwide opinion within hours. The more significant it is, the more people will share it, and the faster it will spread. <br />
<br />
It's significant that the Nada clip is from the viewpoint of a participant in the conflict, not a journalist. Last week, Iranian authorities tried to censor coverage of the protests by essentially banning the press. That situation incubated a powerful new kind of social reporting, one that more closely resembles folklore than the cautious tones and editorial review of traditional journalism.<br />
