													<h2>中枪倒下流血死亡，少女内达成为伊朗的天使</h2>
						
						
																											<div id="postmessage_133152" class="t_msgfont">苹果日报／伊朗「德黑兰之春」遭血腥镇压了。她胸口中枪倒下，年轻的生命在血泊中没有了气息。她的死亡短片，在网上流传，控诉了伊朗独裁政权的残暴，震惊世界。她瞬间成为德黑兰之春对抗极权的象徵，人们为她哭泣，为她悲伤，为她悼念。她的名字叫内达，波斯语的意思是「呼声」，她的死呼唤了人们更坚决争取民主自由的决心。她被封为「伊朗的天使」，守护着德黑兰之春。<br />
<br />
她，伊朗的天使<br />
内达震憾世界的死亡短片，最初由一名居住在荷兰的伊朗侨民上载到交友网站Facebook。侨民声称，短片是他一名在德黑兰的医生朋友传送给他的，而他的医生朋友就是在片段中有份抢救内达的男子。他说，少女全名是内达．苏丹尼（Neda Soltani），是哲学学生。<br />
<br />
子弹击中胸膛两分钟夺命<br />
昨天（周一），再有跟内达有关的短片放上网。在YouTube的一条短片，显示内达跟一头白的父亲，上周六参加反政府示威游行，他们静静地走着，没高喊口号。之后，民兵将她射杀了。在另一条短片，一名说自己在场的医生（未知是否上述那名医生），讲述了内达被射杀的经过：「子弹明显击中少女，枪手瞄淮她的心脏开枪。我是医生，即时n上前尝试救她，但那枪击威力很大，子弹在她的胸膛爆开，不消两分钟，她断气了。」<br />
<br />
年轻生命遭滥杀，使她成为德黑兰之春的象徵。网民纷纷在网上留言为她哀悼，Facebook更设了网页悼念她，网页的名字叫「伊朗的天使」。Twitter满是悼念她的留言：「内达，看着你蚕伦詈笠豢谄，全世界都为你哭泣。你不会白白牺性，我们会记得你。」「他们虽然除掉内达，但除不去她的声音。内达是所有人的姊妹、所有人的女儿、所有人追求自由的呼声。」<br />
<br />
生前攻读哲学于旅社兼职<br />
内达死后仅数小时，美国洛杉矶和纽约市就有示威者举起印有她肖像的海报抗议，其中一款的设计类似美国总统奥巴马去年的竞选海报。<br />
<br />
内达的身份，昨天也大白了。最初有传媒指她是16岁少女，之后证实她27岁，是哲学学生。网上也上载了她的护照照片。跟她家人熟稔的消息指，她在一间旅社兼职，她死后，当局不淮她家人替她举行公开葬礼。<br />
<br />
对于当局滥杀无辜，反对派领袖、前总理穆萨达（Mir Hossein Mousavi）的年轻支持者，在网站号召昨天手持缠上绿色丝带的黑色蜡烛游行，悼念内达和过去一周的牺牲者。他们又呼吁驾驶者在当地时间昨午5时起关掉车头灯，以示「跟烈士的家人团结在一起」。<br />
<br />
悼念烈士，对于在伊朗状蠖嗍的回教什叶派教徒来说很重要，他们在死者去世后3日、7日和40日都有特别的悼念仪式。但这些仪式，在政治上往往激发更多n突。在1979年伊斯兰革命开始时，首次警民n突有2人死亡，在40日后的悼念集会再触发n突，死亡人数更多，最后革命规模像雪球般越滚越大，最终令王室倒台。<br />
<br />
今次内达的死造成极大震撼，加上同日还有约10人在镇压中杀死，这些烈士的悼念活动，不排除会成为当年伊斯兰革命时的翻版，令当地乱局进一步升级，当中以第40日悼念活动的规模最大。美国广播公司（ABC）报道，当局为防内达的葬礼会引发大示威，所以她死后不久已被埋葬。<br />
<br />
听听伊朗人的心声：恶民兵说打就打<br />
<br />
亲政府民兵疯狂镇压，19岁的她手无寸铁，也被打不留手。上周六她与群众游行往自由广场，民兵拦着他们去路，要他们掉头「快跑」。一个身形硕大的民兵，这时凶神恶煞往她走过去要殴打她。她向民兵怒吼：「你要打我吗？」民兵说：「是。」之后毫不手软一棍棍痛打她。她边逃跑边用相机拍照，另一民兵要她交出照片，她把一张空白的记忆卡交出，成功瞒天过海。真正有照片的记忆卡，就交给美国传媒发放。<br />
<br />
听听伊朗人的心声：好警官不忍下手<br />
<br />
民兵和警察中，也有好心人，不忍向示威者下毒手。一名示威者说，上周六游行时，有警员叫他们放下武器回家去，一名民兵更问他：「你为甚么在这里？你不像滋事分子啊！」另一批示威者遇上10多名警察，他们向当中的警官掷石，警官却没下令镇压，反而向他们大喊：「我也有妻儿有家室，不想打你们，请你们回家吧。」但更多的民兵和警察恶待示威者，有民兵在小巷埋伏，等示威者退守躲进来时，就痛打一番。<br />
<br />
听听伊朗人的心声：伊学者敢当流氓<br />
<br />
「我是半个大学教授，半个流氓。」化名普尔吉夫（Besandiar Poorgive）的伊朗学者说。他上周六和学生一同示威，按捺不住对独裁者的怒火，拿起砖头向防暴警察掷去，他「从未想过自己会这样，觉得掷得很差劲，要练习一下」。虽然自觉成为半个流氓，但能够公开抗议政府，他觉得胜利了，尤其是看见防暴警察不懂用水炮，水炮射不出水，射出也只有淋花般水力的时候。</div>

							
							
							
							
