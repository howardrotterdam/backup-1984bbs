													<h2>习近平内定进军委 十八大核心渐成形</h2>
						
						
																											<div id="postmessage_811405" class="t_msgfont">习近平内定进军委 十八大核心渐成形<br />
<br />
作者：陈斐 2010-09-02 13:07:13<br />
<br />
【多维新闻】中国共产党决定10月份在北京举行十七届五中全会。多维新闻综合多个高层消息均证实，政治局常委习近平在五中全会出任中央军委副主席已成定局。虽然自十七届二中全会开始，就一直有传言说习近平将出任军委副主席，为十八大接任胡锦涛担任总书记布局，直到四中全会时，这种传言更盛。<br />
<br />
接近中共中央高层的人士向多维新闻透露，习近平在十八大接任总书记已达成共识，而目前习近平已经是中共政治局常委、中共中央党校校长、国家副主席。在中共党政军三位一体的权力格局中，独缺军职，五中全会补选为军委副主席的时机已经成熟。一位中央党校的高层表示，倘若他这次仍未进入军委，将是极不正常的现象。其中缘由如果不是中共要进行重大政治接班制度改革，调整以往惯例，就是在“继承人”问题上出现了变数。但从目前情况看这些可能性极小。<br />
<br />
进军委料毫无疑问<br />
<br />
实际上，早在今年年初，从中央军委的一些变动就可以看出习近平进军委的安排开始展现。1月29日，中共中央总书记胡锦涛与国务院总理温家宝，以及李长春和周永康一同接见了参加全国边防工作会议的代表。在这次接见的报道中，中央军委副主席的排位有了一个微妙的变化。即第二副主席徐才厚的名字，出现在第一副主席郭伯雄的名字前面，并且一直没有作为“错误”来纠正。因此有人猜测，当时郭伯雄已经开始对实际权力做移交，而且可能在从3月到10月的时间里，中央军委副主席之位将转由习近平接任。<br />
<br />
而且相对于胡锦涛接任中共总书记之前的表现，习近平近两年在国家外交和内政上动作频频，言谈举止方面俨然已有“一号”风范。尤其引人注目的是，今年以来习近平在党校的几次讲话，都直指党风建设的核心点。比如谈到了“大力整治文风会风，提倡开短会、讲短话、讲管用的话，力戒空话套话”，言词分量颇重。再比如，对党史方面的争议更是一锤定音地表态：“反对丑化党史”。这些都在大陆媒体上占据了大量篇幅，并引起党内外热议。习近平在主导意识形态方面的言论，也将是中共十八大高度关注的问题，表现出他已具备了最高领导人的能力。<br />
<br />
此外，习近平更主抓了一些党内外的核心工作，主导了多项党政改革，其中包括推进人才战略规划。在5月下旬召开的全国人才工作会议上，继胡锦涛、温家宝讲话后，习近平主持了后半场会议并作了总结发言，被认为其执政团队形态和执政风格已在酝酿成型。此外，习近平密集地会见访华的外国高官，并出访亚欧等多个国家，均是为即将继任胡锦涛的职务作外交准备。不少参与这些会议的省部级领导都对习近平的能力表示认同，并对他在会议上的脱稿讲话水平给予高度评价，认为他的执政能力已经具备国家最高领导人的潜质。<br />
<br />
更耐人寻味的是，据接近中共高层的人士向多维新闻表示，近来数位以往需由政治局多人商量决定后方能任命的高官，已由习近平直接拍板定夺。尽管上述人士并未透露几位高官的身份，但习近平的独特地位可见一斑。这一姿态无疑说明了中共高层之前拟定的接班人规划在顺利前行。<br />
<br />
其实，习近平与军队颇有渊源。1979年4月清华大学毕业后，习近平即被安排到国务院办公厅和中央军委办公厅工作，成为现役军人，担任了时任中共中央政治局委员、国务院副总理、中央军委秘书长耿飙的秘书。而现在的政治局常委里面尚未有人具有军人的履历。这说明习近平本来就与军方有着良好的关系，在加上其家庭的背景，让他在军界有相当大的优势。<br />
<br />
对于习近平为何在中共十七届四中全会未出任中央军委副主席之事，外界亦有不少猜测。曾有港媒去年载文揭示，十七届四中全会确有中央军委选举安排，准备提请习近平出选中央军委副主席。但在四中全会召开前夕，习近平给中共中央全会和胡锦涛写了一封信，内容大意是他本人到中央工作时间不长，许多情况还较为陌生，尚不具备承担新工作的能力和条件。不过，这一说法并未得到证实。但依循中共通常的工作安排程序及惯例，习近平如此处理重大工作安排的可能性不大。<br />
<br />
事实上，习近平出任中央军委副主席是2007年10月中共举行十七大时的既定安排。十七大的中共中央军委，由上届的“一正三副”模式改为“一正两副”，在副主席曹刚川卸任后，并未补缺，应是预留了一个副主席的位置待补。而胡锦涛是在1992年中共十四大出任政治局常委，直到七年后的1999年十五届四中全会才出任中央军委第一副主席。因此，习近平在五中全会进入中央军委并不晚。<br />
<br />
接班人的必由之路<br />
<br />
外界对习近平是否进中央军委如此关注，是由中国特殊的政治模式决定的，这标志着中国未来领导人地位的确定。按中共近20年来形成的政治惯例，一届中共全国代表大会的中期，就开始为下届作准备。而最早的准备工作就是组织准备。而组织准备的启动象征，正是中共中央军委组成人员的调整。因为中共素有“党指挥枪”的政治原则，所以两代领导人的交接，中央军委变动得最早，中央军委主席之位又换得最晚。所谓最高权力过渡，中央军委是重要载体。<br />
<br />
从大陆的政治体系来看，中华人民共和国中央军委和中共中央军委虽不同体系，却是一组人马、两块牌子。中共十七届代表大会的中期，是按程序决定军委副主席的人选，以及增补中央军委委员。这一决定和增补，实际上是为下一届中央军委，建立基本的交接班子。由于中国最高领导人最终将出任军委主席，也由此构成了核心领导人交接的格局。也就是说，进入军委将是确定习近平成为接班人最重要的标志。<br />
<br />
倘若此次十七届五中全会习近平仍没有进入军委，就可能被猜疑为不符合惯例的极不正常行为。如果这种情况出现，原因可能有两种：一是军委领导班子也希望形成某种民主安排，不希望有那种内部决定的印象，所以希望等待十八大接班后才直接进行替换。这可以说明中国军队事务已经很成熟，并不需要什么熟悉期。但目前看来，中共党内民主改革还不会触及这个层级。军委的工作将非常谨慎，暂时不会在最高权力的过渡上作出如此大的变革。<br />
<br />
二是习近平接班人的地位出现了动摇。因为江泽民的上台，是在六四事件的特殊政治背景下，由元老协商出来的；而胡锦涛的上台，是由邓小平隔代选出、长期备位的，这都与现在习近平成为接班人的背景不同。习近平当年进入政治局常委除了获得江泽民和胡锦涛的认同，更重要的是在党内投票中所获取的票数最高。特别是不少“老人”都对习近平的出任非常认同，所以才水到渠成。因此，除非出现重大错失或发生特殊突发事件，否则习近平的接班格局不会有重大调整。至此，除了在处理香港事务上有些微失误外，习的表现均被中央高层所认可。<br />
<br />
十八大权力角逐提前启幕<br />
<br />
习近平的政治仕途可谓一帆风顺，特别是近几年，他从地方大员一举跃入由九人组成的中共中央政治局常务委员会，并出任中央书记处书记，进入中共最高领导层。2008年3月15日，在第十一届全国人大一次会议上，习近平又当选为中华人民共和国副主席。<br />
<br />
在现有的政治局常委里面，习近平的自身实力、家庭与政治背景等本钱都是十分充分的。<br />
<br />
对于整个中国的政治前景来说，中国即将到来的政治交替最关键的问题之一是，习近平和李克强被安排接替胡锦涛和温家宝的继任者，是否能够适应他们的核心领导角色。一些批评声音认为，习近平与第五代领导人中其他高层人物相比，他的能力和领导才能还有待检验。习近平的某些言论，例如在去年访问墨西哥期间，批评西方政治“干涉中国内政”的严厉言论被很多人视为“不懂外交”和“不像政治家”。有些人甚至希望从目前的省级大员中找出最高领导层的有力竞争者。<br />
<br />
这些想法恐怕只是一厢情愿，习近平的“储君”地位已是中共决策层的共识，几乎没有讨价还价的余地。但确实有些传言认为，原先内定十八大后接任国务院总理的李克强，可能因能力不足，或改为接替人大委员长职务。目前，接任总理呼声最高者是现任主管经济与金融的副总理王岐山。这样，如果仅是习近平在十八大接任总书记，那么十七大时所定的“双接班”就将破局。<br />
<br />
此外，虽然省级大员暂时不会问鼎最高领导权力，但在这些人当中依然存在不少能人，也将会影响中国未来的政治版图。尽管北京领导人的大换班感觉仍很遥远，但政治家们却已经开始行动。在主竞争者之间以及他们所在的派系之间，对于权力的激烈角逐至少在“领导权更替的重要年度”之前两年就已经升温。<br />
<br />
因为中共最近制定的规则和标准的作用，要求高级官员在获提升之前至少需要任职两年。想进入新的中央政治局的候选者在当前及未来的数月中必须有所表现，在中央或省级领导层占据党和政府内的晋升阶梯。这是构成中国政局最富谋略的时期，亦是透视中国政治改革与政策走向的重要阶段。<br />
<br />
（陈斐 撰稿）</div>

							
							
							
							
