 <br />
&nbsp;&nbsp;<br />
8月号的香港「开放」杂志刊文说，中共10月份在北京举行17届5中全会，习近平可能进入中央军委会担任军委副主席。<br />
<br />
17届4中全会前，外界就盛传习近平将成为中共中央军委副主席，但后来未成事实，5中全会习近平是否进入军委会还有待观察。<br />
<br />
习近平是中共内定18大接替胡锦涛担任总书记的接班人，在那之前先担任军委副主席，是沿习胡锦涛当年接班的模式。<br />
<br />
胡锦涛被邓小平钦定为隔代接班人后，自1992年起先后成为政治局常委、中共中央党校校长、国家副主席，而在1999年15届4中全会成为中共中央军委副主席。<br />
<br />
目前习近平已经是中共政治局常委、中共中央党校校长、国家副主席，在中共党政军三位一体的权力格局中，独缺军职。<br />
<br />
「开放」的文章说，习近平已被决定为中共18大筹备工作领导小组组长，5中全会补选为军委副主席是合适时机。<br />
<br />
至于军委主席，文章表示，当前中共中央正在军委和中央委员中统一思想，支持胡锦涛在18大留任军委主席。今后总书记卸任后，继续留任一届或半届军委主席，会成为中共的一项制度。<br />
<br />
与此同时，原先内定18大后接任国务院总理的李克强，届时或只能接替吴邦国的人大常委会委员长职务。文章说，这已是中共决策层的共识，「几乎没有讨价还价的余地」。<br />
<br />
这样，17大时所定的「双接班」就将破局，仅习近平在18大接任总书记，李克强因能力不足当不了总理。文章指出，现在接任总理呼声最高的是副总理王岐山。<br />
<br />
如此一来，这将是中共总书记胡锦涛团派人马的重大挫败。<br />
<br />
此外，被归为团派的广东省委书记汪洋，因为广东近年事件不断，政治前途「越来越没戏」。<br />
<br />
而当前政治局里能力最强的重庆市委书记薄熙来，可能也当不上总理，文章说他有可能调任广东，18大不进政治局常委会，因为「谁敢用他当总理」、「胡锦涛甚至不敢把他留在北京」。</div>

							
							
							
							
