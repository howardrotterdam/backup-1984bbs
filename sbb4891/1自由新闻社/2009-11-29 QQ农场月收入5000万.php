																											<div id="postmessage_347384" class="t_msgfont"><a href="http://www.cnbeta.com/articles/98827.htm" target="_blank">http://www.cnbeta.com/articles/98827.htm</a><br />
<br />
破解“偷菜”利益链：QQ农场月收入5000万<br />
或许当初连“开心农场”的开发团队“五分钟”也没有想到，一年之内一场热火朝天的全民“偷菜”运动正在全国兴起。农场游戏全面的流行造就了一个新兴的市场，而新兴市场的背后则蕴藏着丰厚的利益。五分钟、人人网、开心网、腾讯……还有为代人偷菜而滋生的网络钟点工，巨大的利益链条中，究竟谁是最大的受益者？<br />
<img src="http://i46.tinypic.com/5pqbs.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
全民“偷菜”造就大市场“化肥、狗粮”都得花钱买<br />
<br />
一句“你偷菜了吗？”正成为时下网民乃至人们在现实生活中最常挂在嘴边的话题。从七八岁的小学生，到二十多岁的年轻白领，再到已过知命之年的中老年人……当今开心农场的流行，已是人们不争的事实。<br />
<br />
虽然并不像另一款广受欢迎的网游魔兽世界那样，需要用人民币购买点卡才能进行游戏，但免费玩绝不意味着开心农场是“花钱赚吆喝”。事实上，正是开心农场免费进行游戏的低门槛，为其吸引来成千上万的玩家，而有了这成千上万的玩家，也就有了心甘情愿为农场花钱的主。为什么？“要想玩得比别人好，就得花钱多买道具。”自称农场资深玩家的初中生王浩(化名)对记者说。<br />
<br />
“QQ会员每月10块钱，黄钻每月10块钱。”王浩开始向记者细数自己耕耘QQ农场所要付出的现金费用。据王浩介绍，虽然QQ农场不收费，但他花的钱并不比玩付费大型网游少。“化肥、狗粮都要用Q币购买，这样自己的菜才不会被人偷。”据记者了解，Q币是通行于腾讯网内的一种虚拟货币，其与人民币的对价基本是1：1。“狗粮0.4Q币一天，化肥有好有差，中等的高速化肥要1Q币一袋，一天用两袋化肥就是2Q币，但是有黄钻可以全场八折……”王浩最后得出的结果是每天花费1.92元，这样连同会员费、黄钻特权费，他一个月在QQ农场上要投入80元左右。<br />
<br />
王浩告诉记者，家里每月会给他100块钱零花钱，主要是让他上午饿了买点东西吃。但“为了农场，只能牺牲一下了”，王浩有些自豪地说：“我是班长，农场级别也是最高的！”<br />
<br />
在采访中，记者还了解到，王浩的很多同学都与他一样，将自己的零花钱大把地花在农场游戏上。无独有偶，白领之中花钱买道具、练级别的亦大有人在，经济条件更加宽裕的他们往往还在休假时雇用网络钟点工为其种菜收菜。相反，很多中老年玩家倒是不愿为玩游戏花钱，只是本本分分地种菜收菜。<br />
<br />
浪潮集团的一位业内人士告诉记者，由于运营平台很多，加上不少的山寨版本，要精确得出开心农场的总收入并不容易。但过亿的用户、数千万的活跃玩家，再将游戏道具的具体价格计算在内，即使做最保守的估计，开心农场所造就的市场肯定会达到亿元这个数量级上。<br />
<br />
从开发到运营利益多 替人“偷菜”可赚代理费<br />
<br />
开发、 运营、 销售，三者加 在一起构成了开心农场完整的利益链条。除此之外，因代替别人偷菜而产生的网络钟点工，也可以从中分一杯羹。面对价值数亿元的蛋糕，各路商家是如何瓜分的呢？<br />
<br />
首先，从网络钟点工的角度来说，其非官方的本质决定了他不可能得到数额巨大的利益。“我们是游离于体制之外的，只有在玩家休假又没有时间种的时候代劳。” 一位曾在“十一”期间代人收菜偷菜的网络钟点工通过网络告诉记者，“也就是当个副业，一天赚个百八十块钱就不错，而且一个月就那么几天。”这位网络钟点工还告诉记者，与大型网游相比，开心农场的代劳费少得可怜，“魔兽世界的一个好战士，可以卖到几千。”<br />
<br />
想要从代玩游戏中获取暴利已是不可能，但是玩家想要玩好开心农场，就要购买道具，想要购买道具就得用现实中的货币按一定比例购买游戏中的虚拟货币。那么游戏卡的销售商是否从开心农场的流行中大赚了一把呢？<br />
<br />
郭伟(化名)是土生土长的章丘人，和妻子从老家来济南城里打拼已经快10年了。起先他们烙过煎饼、卖过熟食，4年前在北园开了一家以销售各种游戏卡、手机卡、充值卡为主的小店铺。当记者问起游戏卡的销售生意如何时，郭伟摇摇头说：“生意不如以前好做了。”据郭伟介绍，每销售一张售价30元内含 30Q币的游戏卡，他只能从中赚一块五毛钱，收益高不高，全部看销量。“以前量走得还行，这附近又有几个学校，一个月几百张卡没问题，” 郭伟回忆说，“但现在很多孩子都会用手机、网银买东西了，那样还便宜”。郭伟告诉记者，游戏卡远没有手机卡、充值卡好卖、利润高，所以很多商铺和他一样都以卖手机充值卡为主了。<br />
<br />
“菜农”数量几何级增长 QQ农场月收入5000万<br />
<br />
2008年，以校内、51.com为代表的SNS(社交网络)网站推出开放平台，国内SNS应用兴起，正是在这种大背景下，五分钟公司开始了SNS游戏的创作。而2008年底，五分钟开发的开心农场率先在校内网上线了。<br />
<br />
但在巨大的利益面前，校内网和五分钟却出现了分歧。据悉，校内网曾希望开心农场只在自己的平台运营，但这一建议遭到了五分钟的拒绝。2009年2月和3 月，开心农场分别在校内网的竞争对手漫游平台和51网上线，这引起了校内网的不满。于是，校内网请来了另一个团队智明星通开发了类似的游戏开心农民，与五分钟版本的开心农场分庭抗礼。<br />
<br />
或许，校内网与五分钟之间的恩恩怨怨只能算是小打小闹，腾讯的进入，彻底改变了开心农场的利益格局。<br />
<br />
2009年4月，已经在校内网、开心网火爆流行的开心农场，被腾讯改名为QQ农场后正式上线，之后其用户数量呈几何级增长。<br />
<br />
在香港上市的腾讯控股的三季报告中，记者注意到：腾讯“QQ空间”的活跃账户在三个月间猛增33.7%，于9月末达到3.053亿。对此，腾讯表示，如此迅速的增长主要由于推出了新的社交网络应用广受用户欢迎。显然这个“基于社交网络的休闲游戏”主要是指QQ农场。<br />
<br />
“和腾讯相比，人人也好，开心也好，都是小菜一碟。”浪潮集团的这位业内人士告诉记者，一般的SNS应用在人人网、开心网上是通过5：5的分成来划分开发者和营运商的收益的。开心农场在人人网上的月收入是100万元左右，这样人人网与五分钟就各得50万元。加上其他网站的运营分成，五分钟的月收入估计在 300万元左右。<br />
<br />
