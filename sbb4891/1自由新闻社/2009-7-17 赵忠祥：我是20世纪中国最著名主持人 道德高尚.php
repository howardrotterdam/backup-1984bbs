																											<div id="postmessage_165832" class="t_msgfont">来源：搜狐&nbsp;&nbsp;<a href="http://media.sohu.com/20090717/n265283319.shtml" target="_blank">http://media.sohu.com/20090717/n265283319.shtml</a><br />
<br />
　　赵忠祥：我是一个道德很高尚的人<br />
<br />
　　我在文艺界里面是没有离过婚的啊。我没有在这里数说别人谁离婚谁不对，我绝没有这个意思，你离婚更对。我既然没有离婚，说明我是能够包容我这个家庭的，我妻子也是能包容我的<br />
<br />
　　6月25日晚上，上海浦东，东方电视台演播厅。67岁的赵忠祥衣履考究、妆容略浓，他小心翼翼地攀爬上后台一个两米多高的梯子，等候《舞林大会》第三期的录制开场。<br />
<br />
　　音乐声渐起，赵忠祥和着节奏，将手里的麦克左右摇摆；他的状态来得很快，几乎是说来就来，无需任何准备。片头放过，赵忠祥顺着这个并不宽敞的梯子登上舞台中央，虽不能说“健步如飞”，却可以说“老当益壮”；他字正腔圆地开腔：“各位父老乡亲，电视机前的观众朋友们，大家晚上好!”一如过去他主持过的任何一场“春晚”或者“心连心”。<br />
<br />
　　年近7旬的赵忠祥重出江湖，并投身娱乐节目，同台湾“综艺天王”吴宗宪搭档，在海派文化气息浓郁的东方卫视主持青春动感十足的《舞林大会》。这两个“不搭调”的男人走到一起，被视作翻版的“周杰伦配费玉清”。几期下来，收视率很是可观，使得在上海滩并不受人待见的东方卫视“扬眉吐气”。<br />
<br />
　　东方卫视副总监徐向东说，他和东方卫视怎么都没有想到：“赵忠祥”3个字到了21世纪的今天，还会有如此之高的眼球吸引力。<br />
<br />
　　赵忠祥无可争议的是曾经一个时代的播音“主流”，用他自己的话说，那叫：“庄重，严肃，字正腔圆，掷地有声，气壮山河，凛然不可侵犯。”他用这种“主流”播了25年新闻，主持了15届央视春晚。他从没有觉得这种延安革命广播时期形成、他和他的同事们继承并发扬的“中国播音学”有任何的过时，他坚信这仍是不可更迭的声屏主流。<br />
<br />
　　在2004年以前，赵忠祥绝对是央视乃至全国电视圈标榜“德艺双馨”主持人的不二人选，就连小品里都有“我十分想见赵忠祥”的经典台词。<br />
<br />
　　2004年，央视女保健医生饶颖曝出与赵忠祥长达7年的“地下绯闻”，并出示了流产证明和赵亲笔书写的欠条。赵忠祥从此深陷泥潭，难以自拔。这个无数国人曾经的偶像轰然倒塌，赵忠祥由 “主流”瞬间被指为“风流”，甚至“下流”。<br />
<br />
　　此后的赵忠祥，无论是在央视，还是在国内其他电视台，出镜率大幅下降，偶尔的几次露脸，国人皆呼其“苍老得厉害”。对于绯闻，赵先是屡屡伺机雄辩，后长时间地保持缄默，如今借复出和新书《湖畔絮语》的出版，他再度开口，大有“谁怕谁”的架势。他说这是“对丑行有责任抵制，在与其过招中，给善良人们做一警示”，“无论天地正气还是堂堂我心都‘两岸猿声啼不住，轻舟已过万重山’；‘洛阳亲友如相问，一片冰心在玉壶’。”<br />
<br />
　　<strong>他特别感恩文怀沙</strong>，当年文在钓鱼台与赵相遇，赠言“前一段时间有人在我面前说长道短，我只有一条原则，谁说忠祥不好，谁就是坏蛋”。他特意说起饶颖指证的当年的事发地点“丰台区怡海花园”：“如果丰台区真有怡海花园小区，谁能证明我去过那小区，我赔一万元。谁能证明那小区的哪个房子是我或我家人至亲的，这栋房子我白送给他。”<br />
<br />
　　离开央视，加盟《舞林大会》，赵忠祥算是真正涉足娱乐圈了，这还包括心态上的。如今，谈及再敏感的话题、再八卦的传闻，他都能话如泉涌，毫不闪烁其辞：<strong>“小八卦我还能忍，说什么没关系，只要你最后别说成我叛国、叛党、投敌就行，其他都没关系，我也不会计较，比如说赵忠祥是个大混蛋、大流氓，都可以，我不会生气。”</strong>“八卦到我身上来是非常容易的，我根本就不理他，我觉得我经得住。因为赵忠祥这3个字在中国人民心里是有分量的。”（南方人物周刊）<br />
(责任编辑：张洪飞)<br />
