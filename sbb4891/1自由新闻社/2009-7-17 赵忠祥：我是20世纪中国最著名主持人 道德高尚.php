													<h2>赵忠祥：我是真正的正人君子</h2>
						
						
																											<div id="postmessage_166962" class="t_msgfont"><p align="center"><strong>我是20世纪中国最著名的主持人――对话赵忠祥</strong></p><br />
<br />
<p align="center">作者: 南方人物周刊</p><br />
<p align="center">2009-07-17 17:28:10</p><br />
<br />
<a href="http://bit.ly/1a5tc5" target="_blank">http://bit.ly/1a5tc5</a><br />
<br />
<p align="center"><img src="http://i31.tinypic.com/souans.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /></p><br />
<p align="center"><strong>夕阳红 老赵现在称得上是野鹤闲云</strong></p><br />
<br />
<strong>在哪儿都是等死，健康就为干活</strong><br />
<br />
人物周刊：不久以前，你在央视办结了退休手续。现在，已经不再是央视主持人的你加盟东方卫视《舞林大会》。“不得走穴”的紧箍咒从此对你失去了意义。你是退出央视而不休于荧屏啊。<br />
<br />
赵忠祥：最早我们全是铁饭碗，毕业后分配到一个地方，这个地方规定你到60岁就退休了，然后这个机关就一直养着你。我有的同事，年轻的时候就说，这辈子我就赖着电视台了，最后让电视台送他一个大花圈。实际上现在这种模式已经不是这样了，很多主持人都是签约性质的了，这个约你不一定能签到60岁，也有可能到70岁，人家美国70岁、80岁华莱士还在签约。“有的人活着，他已经死了；有的人死了，他还活着。”<br />
<br />
人物周刊：你觉得，你的能量还没有穷尽？<br />
<br />
赵忠祥：煤矿工人，到了60岁、70岁，还让人家去干，是不可能的，也不应该的。但是对老中医来讲，70岁可能是最黄金的时代了。我们能去说，老爷子，您别干了，即使您开一副药就能救人您也别做了，能吗？<br />
<br />
人物周刊：有没有想过，到年龄了，该在家颐养天年了？<br />
<br />
赵忠祥：我在10年前见到孙道临，我们很熟，他那个时候比我现在的年龄还大，我在后台看到他，我就说孙老师，您这么大年纪了，坐在家里多歇歇多好啊。他说，难道一个人非要坐在家里等死吗？<br />
<br />
<br />
<p align="center"><img src="http://images.infzm.com/medias/2009/0717/27758.jpeg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /></p><br />
<p align="center"><strong>从央视退休，赵忠祥心情不错　图/雍和</strong></p><br />
<br />
人物周刊：10年后，你认同了孙道临的话，你也不愿意“在家等死了”。<br />
<br />
赵忠祥：哈哈，在哪儿都是等死吧。只要你自己还能活动，坐到飞机上跟坐到沙发上没有什么区别，你只要把心态调整好，跟他们去舞林工作，以及坐到你这儿接受采访，也没有什么区别。<br />
<br />
<strong>《新闻联播》，舍此无其他</strong><br />
<br />
人物周刊：罗京的去世，报道和悼念的规格相当之高，这引起了不少人的质疑。你怎么看？<br />
<br />
赵忠祥：你看联播才能知道我们国家的主流意志是什么，你必须看，它占据了这样一个要津地位。他的离去，必然会让更多的人知道，为他惋惜。反面来看，他的规格这么报，我尽管是中央电视台的人，我认为这越位了。你能明白我的意思吗？他的身份不应该在新闻联播里报，这是破格。<br />
<br />
人物周刊：你的理解，这是破格？<br />
<br />
赵忠祥：一个国家的体制、仪式，从封建社会来讲，都是有规定的，是有制的，一级一级的，没有一个国家没有级别。罗京这次，《新闻联播》里面为他不是报了一次，而是几次的报道，我觉得超过了党和国家一般领导人的规格。<br />
<br />
人物周刊：你如何看待这次的破格呢？<br />
<br />
赵忠祥：这产生的问题是：其他工种的同志以后去世了，《新闻联播》要不要这么报？如果都这么报，那没问题。如果不是都这么报，就有问题。我只能这么理解。这里头丝毫没有我对罗京有任何看法。即使是我死了，我觉得也不应该破格。这跟迈克尔・杰克逊去世了不同，现在全世界的歌迷对他的悼念形成了几乎是戴安娜之后的一个更大的高峰。这个高峰，你不管在哪个新闻节目中去报，你是报道社会上形成的这种高峰，这是客观形式的报道，我们去报这样一个消息，是客观报道，不是我们的造势。<br />
<br />
人物周刊：罗京去世的报道是一种造势吗？<br />
<br />
赵忠祥：我不认为对他报了就怎么着了，我丝毫没有这样一个意见。主要是，这样一种处理是否能形成一种常态，如果是常态未尝不可，如果不是，那么他的特殊性的理由是什么，我们不知道。外界可能有意见，《新闻联播》是你们自己掌控的，所以不是联播的人，可能死了就不会有这么大动静了。当然，这都无所谓。人一闭眼就什么都不知道了。千秋万岁名，寂寞身后事。<br />
<br />
人物周刊：那么，你对罗京的评价呢？不造势地评价。<br />
<br />
赵忠祥：他的工作在他的组里是超人一等的，据我看。他跟他们播音组现有的男生比，不跟女生比，他是突出一块的，其他的跟他比还差一截，不能说跟他并驾齐驱，到不了。那是由于他的先天素质、后天努力。他跟张宏民、王宁比，他要更好一点，所以重要的事件都由他播，这已经形成了一种格局。这种格局就是一个标志。但是，好死不如赖活着。《新闻联播》就算播一百条，也不如我们不死的。<br />
<br />
人物周刊：罗京走后，很多人在议论《新闻联播》变脸的话题，认为联播这么多年来形成的播音风格应当改变。你曾经是联播25年的老播音员，如何看这个问题？<br />
<br />
赵忠祥：这是不可能改的，一个机器的行进，车上掉下来一个人，旅客的时刻表就改？不会的。罗京尽管起到非常重要的，甚至说是栋梁之材的作用，也不会因为他的逝去使大殿将倾。这种播音风格不是一个人两个人说了算的。<br />
<br />
人物周刊：谁说了才算？<br />
<br />
赵忠祥：它是一批人定下的，已经印在人们的头脑里了，而且并不从电视始，是从电台开始的。由老一代播音员齐越、夏青他们创造的字正腔圆、正襟危坐、大义凛然的这么一种播音声势，已经形成了中国播音界的一种共识，没有人认为这是不对的。庄重的、严肃的、字正腔圆的、掷地有声的、气壮山河的、凛然不可侵犯的。你是一个大国呀，你要代表你的一个国体呀，如果你一个大国的人出来都是跟小沈阳似的，说话嗯啦啊啦的，4个字：成何体统啊！<br />
<br />
人物周刊：你觉得不能改？<br />
<br />
赵忠祥：我觉得，中国的国情要求我们的主持人说话要能够撑起这个国家的体面来，就不能以一种非常个性化的人来代替全民的主流意识。主流意识不是一个人的个体风格随便能来诠释的，那你胆儿也太大了。<br />
<br />
人物周刊：如果改了呢？<br />
<br />
赵忠祥：影响它的严谨性。很多东西，离开了念，我们认为是不庄重的，我凭什么听你在话筒前说，你说什么是什么，这个世界是由你来说的吗？念的话，就告诉你，这话不是我说的，是用一种公文形式来传达给你。<br />
<br />
人物周刊：那么，现有的联播播报方式有欠缺吗？<br />
<br />
赵忠祥：当然有欠缺，欠缺就是亲切不够。语调上可以更还原为“说”的因素，其实我在播《新闻联播》的时候，后期已经意识到这个问题。你没有必要唱高调、绷着脸，用文革期间的那种打倒美帝国主义的态势去播。有理不在声高嘛。<br />
<br />
人物周刊：你强调严肃和亲切，这两者有无矛盾？<br />
<br />
赵忠祥：亲切和严肃的表达方式不存在矛盾。但是字正腔圆，每分钟220个字，而且字后面你不能加语气词这是一定的。为什么？因为新闻的语体。这是全世界的新闻工作者在过去120年间形成的一个非常好的一个格律。<br />
<br />
人物周刊：总结一下吧。现在的《新闻联播》，你个人给它打几分？<br />
<br />
赵忠祥：这是人类目前为止，已经达到精益求精的一个最佳的阶段。不光是中国的《新闻联播》，我一定要讲清楚，我们不要动辄谈欧美，现在欧美日，所有全世界的大电视台，没有一个说新闻的。都是用5个W的简单的这种新闻语言，来向大家传递信息。如果你要“说”的话就一定会加字儿，你加字儿的话就会影响你的信息量，影响你在最短的时间里最高的效率。所以这是最好的一种形式。舍此无其他。<br />
<br />
<strong>我是20世纪中国最著名的主持人</strong><br />
<br />
人物周刊：这次与台湾综艺天王吴宗宪合作，令很多观众着实吃惊。<br />
<br />
赵忠祥：娱乐节目不在此例，因为没有国家娱乐嘛。你听说过这是党中央的娱乐吗？<br />
<br />
人物周刊：东方卫视《舞林大会》获得的轰动，你认为来自节目本身，还是你和吴宗宪的组合效应，或者说单纯来自你个人重返荧幕？<br />
<br />
赵忠祥：从理论上来讲，主持人代表节目的品牌和标志。从我们文艺上的一个老传统来讲，就是“人保戏还是戏保人”。如果你已经和观众建立了一种互动的、非常深的交流的一种关系，那他就爱你没商量。到那个时候，说实话就是到了暗恋的情人那样一个程度，或者说父母溺爱孩子那种程度，隔着窗户看着孙子在那儿玩，眼睛都能笑出眼泪来，脸上都能笑出一朵菊花瓣来。你如果真有这个能力，能建立起来这样一种亲情和态势，那么无论你嬉笑怒骂、摸爬滚打，它皆成文章。<br />
<br />
这20年来，我们没有出现过一个明星，是让我们从心里头尊重他并喜欢他的。爱他有时候不尊重他，尊重他有时候不爱他。<br />
<br />
人物周刊：你觉得，观众对你，是爱你并尊重你吗？<br />
<br />
赵忠祥：有一部分人。但是我没有达到这么高的拥有率吧。这其实是我们毕生的追求。你既然干这事儿，谁不愿意大家喜欢我啊。就像男欢女爱，我去相姑爷去，找对象，难道我愿意她看见我就恶心吗？成不成无所谓，她应该对我留个好印象。不能是她觉得我是大海，她晕船，见到我就要吐吧。我见过可以说20世纪中国所有最有名的艺术家，我觉得那个时候大家对艺术家的喜爱程度，比现在要高尚。<br />
<br />
人物周刊：你说你见过20世纪中国最有名的艺术家。那么，你觉得自己是20世纪中国最著名的节目主持人吗？<br />
<br />
赵忠祥：是的，我同意。著名有很多种著名，臭名远扬也算著名。但是我的出镜率高、我时间长，大家对我知名度的认可，这点我不否认。但是我觉得换位思考，我是一个观众，我现在会不讨厌赵忠祥，会接受赵忠祥。但是，应当还没到爱他没商量的地步吧。<br />
<br />
人物周刊：回到刚才那个问题，现在大家看《舞林大会》，究竟是看节目，还是看你，还是看你和吴宗宪呢？刚才你没有正面回答。<br />
<br />
赵忠祥：上海台请这么一位老爷爷来，绝对不是供一个祖宗。任何单位现在没有这么一个义务。而是它在想有没有我可挖掘的余地。正好呢，弄一个吴宗宪，我并不了解他，他是非常搞笑的，那么我是非常正经的，非把两人弄一起来，等于是“风马牛不相及也”。现在就是风马牛要相及，来一个“狮虎配”，大家也未尝没有这个兴趣。这是我冷静分析这个事儿。<br />
<br />
人物周刊：那么，你们的合作是否愉快呢？<br />
<br />
赵忠祥：我是一个O型血，我跟谁都可以组合，只要他是一个活人。他可以就着我，我也可以就着他，这都没什么问题。事实上，我面临过多次挑战。当年我跟杨澜做正大综艺，她一个小姑娘，大学刚毕业，在台上“叭叭叭”，你一个老同志栽在一个小丫头身上你值吗？我觉得我那个时候冒的风险要比现在大得多。<br />
<br />
吴宗宪是台湾综艺天王，要是哪天我们因为磨合不好掐起来了，我觉得我们彼此都不丢人，不存在我栽在毛头小伙子手上。风格不同，我们试试步，一个慢步一个快步，看能不能凑一块儿；一个喜欢吃开水煮东西、一个喜欢吃冰冻的，看看能不能凑一桌。<br />
<br />
<strong>我是真正的正人君子</strong><br />
<br />
人物周刊：你现在感觉自己幸福吗？<br />
<br />
赵忠祥：在工作上乃至能力的发挥上，我觉得我不光是幸福，而且是幸运的。因为目前在中国健在的播音员主持人当中，哪怕是我的前辈，没有一个人在他的职场生涯中，能够有这么多的各种各样的机会去担纲各式各样的节目，谁也不可能有，包括罗京也不可能有，他只能播新闻。我跟他一样也播了25年新闻，但我播了25年我不播了，他也不播了，他走了，我又去做别的了。<br />
<br />
人物周刊：你一直在说事业上，工作上。生活上呢？<br />
<br />
赵忠祥：生活的幸福叫冷暖自知。家庭的幸福率和不幸福率是由你自己的追求和自己的满足来确定的，它是一个难以量化、难以细化、难以数字化的这么一个心理学的过程。你家庭里头有糟糠之妻，或者说有一个美若天仙的妻子，哪个幸福？你要真有一个美若天仙的女子，视觉是美了，但她在物质上什么都不会，你伺候她还有一身脾气，然后还移情别恋，你家庭并不是很幸福。家庭的幸福不以外人的观感为指标。<br />
<br />
人物周刊：我们不以外人的观感为指标，就想以你自身的观感为指标。你的生活幸福吗？<br />
<br />
赵忠祥：以一个最主要的指标来讲，我在文艺界里面是没有离过婚的啊。我没有在这里数说别人谁离婚谁不对，我绝没有这个意思，你离婚更对。我既然没有离婚，说明我是能够包容我这个家庭的，我妻子也是能包容我的。我们一直能结伴生活到现在这个程度，3个字：不容易。那我还要求什么呢。夫复何求？<br />
<br />
人物周刊：但是你毕竟有过绯闻。谈到赵忠祥，我想这是绕不过去的话题。你也不能够回避。很多年过去了，别人还是觉得，这是赵忠祥身上的谜。<br />
<br />
赵忠祥：这个问题，恕我直说，我希望你在你的文章中不要写她的名字两个字，你可以用任何名词。我是对事不对人，这个事情如果今天咱们平心静气地说，在目前这个社会当中，一般概念上的所谓的男女关系，或者说不正当的男女关系，你觉得现在它的普遍性和存在的情况，少吗？<br />
<br />
人物周刊：你觉得是多是少？<br />
<br />
赵忠祥：很多所谓的正人君子，其实我都知道他们一肚子男盗女娼。甚至有一个记者，他专门写我的所谓花边新闻，最后他向我袒露：他说他跟一个小姑娘，把人家肚子都搞大了。我说老兄，你怎么能有脸装成正人君子来声讨我这个正人君子呢？这个世界就是这么奇怪的一个世界。<br />
<br />
事情如果没有一种波澜，没有一种风风雨雨，它就不是一个事情。我所以觉得这是20世纪末21世纪初，一批没有经验的或者说没有职业素质的娱记炒作出来的一种现象。这个现象到底最后它是个什么东西，大家谁也没有说出一个准事儿来，到底老赵怎么了？他做了一件什么事儿？他是不是背着他的妻子，跟另外一个女人产生了一段不正当的男女关系，这个女的不干了，跟他要钱，封他口，他不给，然后就闹出事儿来了？就算是有这种事情，咱们假设，我们现在说假设，如果不是老赵，而是他们家门口看门的老大爷的事儿，你觉得是个事儿吗？既然不是个事儿，那么这伤害的是这个社会，而不是我。<br />
<br />
人物周刊：你觉得这个事情自始至终你并没有受到伤害。<br />
<br />
赵忠祥：我不觉得受到伤害，因为如果我觉得受到伤害了，那么是我理亏。事实上，我和我的家庭、我的朋友都没有受到伤害。我跟你披露一个细节：咱们按新闻学的5个W（时间、地点、人物、事件、原因）来讲，某人说，某年某月，我给她打电话，让她上我们家去，然后强迫她跟我发生了关系。这个话，社会上的人怎么说我不管，可能会说“老赵他妈的看上去正人君子，背地里也做这种下流的事情，没出息的事儿”；但其实最受伤害的肯定是你的妻子。你妻子那天肯定没在家，她就会怀疑――怎么能保证没在家的时候，丈夫没勾一个女人进这个屋。对不对？但是你要记住一点，谣言和新闻的区分。新闻要的是真实性，这就在于5个W。<br />
<br />
人物周刊：when、who、where、what、why，你觉得哪一个没对上？<br />
<br />
赵忠祥：时间和地点。她说的这个时间段，正好是我们家在装修房子，我和我妻子住在水电招待所，住了4个月，而且那次挺逗，是一个剧组给我和我老妻报的账。我儿子不能跟着我们住，我在这儿开两个房间一天两天可以，时间长了你开不起呀，所以他住在他姑姑家。这个事儿形成了法律文本：她在法院上就讲，什么时间段，她给我打电话怎么怎么着。后来又一个证据是流产证明，就是因为我去那儿。咱们说，就算你去那儿，这个流产证明也不能证明就是这个事儿，要是你学生理学的你就会知道。更何况，那个时候我们不住那儿。你要说去那儿跟装修队的怎么着了吧还有可能。你千算万算，你跑不出5个W的，我是搞新闻的。时间、地点是非常重要的要素。你把时间、地点锁定了，你要知道这个时间对你来讲发生的是一件事儿，对我们家来讲发生的是另外一件事儿。<br />
<br />
人物周刊：这是你的解释和理解。那么，你的妻子呢？她现在怎么看这件事？<br />
<br />
赵忠祥：我不用跟我妻子去说，这还用跟她去解释吗？因此，我觉得，这种事儿再闹得外头满天飞，我们不理它就完了，其实这种事儿只要家人能够互相谅解了，内部能很团结了，就行了。<u>而这种团结不是克林顿跟他夫人希拉里之间的互相谅解，说有了这事儿我原谅你了，王八蛋你以后他妈的给我老实点</u>。而是，不用解释的一件事情。<br />
<br />
难道你说还用去解释吗？这件事不管外头怎么闹，实际上总的来说对我而言，构不成什么伤害。你要再去编，是1998年了，还是1996年了，随便你去编，但你一定要记住一点：你的谎言总是和事实的5个W是不可能吻合的，这叫天经地义，没有任何巧合的可能性。因为这5个W这5项制约，你得弄一亿多个排列组合才有可能对上，一般的概念上是对不上的。我是搞新闻的。<br />
<br />
人物周刊：你说要弄一亿多个排列组合才可能对上。如果今天真就对上了呢？你又怎么解释？<br />
<br />
赵忠祥：<u>话说回来，对上了那就对上了，再说了，就算是对上了，说某人曾经跟一个女人不慎发生了这种关系。呃，够祸国殃民吗？够出卖情报吗？够反党反社会主义吗？难道你们都没有吗？把这窗帘拉上，大家都没有，出去都是正人君子；拉开以后，他妈都有，尤其那些贪官污吏。因此我非常仇恨那些人，伪君子不如真小人。在这个过程当中，有一些人的脸色，我全都清楚，我心里头有一本账呢，而且我深知他们是怎么回事。真正的正人君子，他反而对这个事情有非常大的宽容度和包容度，有仁爱之心。真正那种乌鸦站在猪身上说别人黑的人，他自己才黑呢。因为我看了很多起哄的那些人，他们一塌糊涂。</u><br />
<br />
人物周刊：你身边怎么尽是所谓的伪正人君子呢？有好人吗？<br />
<br />
赵忠祥：真正非常好的人，我碰到很多女人，很好，四五十岁的老大姐这样的，见了我都说，忠祥，你别往心里去，这事儿主要是跟美珠（赵忠祥妻子）解释好就没事了。她不管这事儿真的假的，她有一个标准，一个行为方式，一个做人的标准，而不是起哄说，“哦，看热闹勒，嘿。”我觉得这是一件事情出来以后，你为什么要把它夸大到这样一个程度，为什么纵容谎言满天飞，大家都跟着起哄，与其信其有，不可信其无，而且希望这事儿闹得越大越好，大家最后看热闹，这种心态和鲁迅那个时代，看革命党人被斩首的时候，大家拿馒头去蘸血的那种感觉没两样。我觉得时间过了一个多世纪了，但是人们残留的这种恶劣的劣根性还存在。因此我觉得，这是很要命的一件事。<br />
<br />
人物周刊：你一直在向我强调，你是真正的正人君子。<br />
<br />
赵忠祥：我可以说，我是一个道德很高尚的人。我不排除我对某女子有过什么非分之想，甚至于有过情愫上的一种眷恋，我要是把自己撇那么干净，也不是一个非常可信的人。但是，第一我忠于我的妻子；第二，我忠于我的家庭；第三，我忠于我的事业。我没干过什么伤天害理的事儿，我怕什么呢？至于别人对我怎么想，我完全可以冷眼看世界。<br />
<br />
人物周刊：绯闻对你的影响有多大？看得出来你很在乎。上个月你刚出一本新书《湖畔絮语》，有三分之一以上的内容是在澄清这件事情。<br />
<br />
赵忠祥：有影响，应当不大吧。其实我跟你说一个很简单的道理，如果上海台认为我是这样一个非常寡廉鲜耻的人，他们应该不会用我吧。另外这件事情别人有什么资格说三道四呢，最重要的是，当事人自己都出示不了什么像样的证据，别人在这儿说三道四有什么意义吗？这件事就对方她一个人说黑的就黑的，说白的就白的，你愿意信就信，不愿意信就不信，你们要能把这事情做实了，你们拿出证据来。到现在为止，没有一个人能挺身而出站出来说，看见过他们俩在一块儿喝过茶，吃过饭，走过道，议论一件事情。没有一个证人。你能相信这个世界上，如她所说，在一起有7年之久的这样一个人，周围就没有一个人能挺身而出给她做个证明吗，可惜没有。我们在法律上不是讲情理而是讲证据的，证据在那儿摆着，你说你不认识她，大家都证明你认识她啊。<br />
<br />
人物周刊：对你的争议，除了绯闻的问题，现在还有钱的问题。网络舆论说你《舞林大会》的酬劳是300万，有人说不止，还有传言说地方台用更高的酬劳挖你。<br />
<br />
赵忠祥：就算我挣了300万，也不为多，我觉得我不止300万的价值。我要去拍一条广告，有可能要得更多。我的话只能说到此。我觉得挺好玩，有一些人不知道一个人收入300万或者500万是什么概念。我认识的一些朋友，代言一个广告，一条就是四五百万，一年会做两三条。<br />
<br />
<strong>我不会有孤独感</strong><br />
<br />
人物周刊：是非纷争如此之多，你会有忧虑吗？<br />
<br />
赵忠祥：最近，我没有什么忧虑。<br />
<br />
人物周刊：为什么现在的心态如此坦然呢？<br />
<br />
赵忠祥：因为我现在衣食无忧。我退休下来，国家要养活我，要给我退休金，医药费要全额报账的，另外我已经有了一套房子，有了一个固定的住处了，未来只要国家没有大的战乱，大的天灾人祸，我想我会安度晚年的。我现在想象不到我会有什么忧虑，也就过马路别让车撞到，或者甲型H1N1流感别让我们这种基础不好的老年人、高危人群感染到吧。<br />
<br />
人物周刊：你一辈子都处于别人的瞩目之下。有一天你离开了荧屏，会不会感到孤独？<br />
<br />
赵忠祥：不会有孤独感，我会写书，会上网。我今天要写一篇博客，跟新浪讲，每天至少十几万的点击率，所以我不寂寞，我有很多种形式。<br />
<br />
人物周刊：我知道你不但会写博客，还会唱《双截棍》。你还真算一个时尚老人了。<br />
<br />
赵忠祥：现在再传统，牛奶你也得喝，咖啡你也得喝。这些生活不是我的常态，现在这个社会你不可能不沾染一些别的东西，炸油条得沾点油啊。<br />
<br />
人物周刊：但是你的博客、你的诗，屡屡受到网友的批驳。<br />
<br />
赵忠祥：我丝毫没考虑过别人，也不管别人怎么想。我做得再好也有人说不好，我做得再不好也有人包容我，这就是大千世界的复杂性。<br />
<br />
人物周刊：既然你不考虑别人，为什么还要放到网上，让所有人都可以评判呢，不是自找烦恼吗？你是不是害怕别人忘记你？<br />
<br />
赵忠祥：写博客、写诗是表达我的心情，大家都可以表述对这个世界的态度。比如我的诗“神七”，第一个对“神七”上天称颂的人就是我，那些批评我的人他们敢谴责我的诗写得不好，但是他们不敢谴责我的爱国之心。难道这一点你都要去谴责，你们干吗呢，你们无地自容吧？我一个老同志，我的诗如果写得不好，但是“神七”上去了，我愿意去歌颂它，你们那些会写诗的人为什么不去歌颂，难道不值得你们去歌颂吗？我们国家好多地方都不值得你们去歌颂吗？只要看到国家不好的地方你们都要去揭疮疤吗？你们是这样的一群人吗？如果你们是，你们批评我，对我的价值构成伤害吗？<br />
<br />
人物周刊：现在的年轻人，特别是“85后”、“90后”，还知道你、喜欢你吗？<br />
<br />
赵忠祥：很多“85后”小孩，寒假中都要来看我的。他们都喜欢我。当然，大观园里的贾宝玉也有人喜欢，也有人不喜欢吧。这个世界上大家全都喜欢的一个事情就是“吃”，不吃就死了，除此以外没有了。<br />
<br />
人物周刊：这辈子，你爱过谁，恨过谁？<br />
<br />
赵忠祥：我所有恨的人都是我感谢的人。只有你恨的人才促使我进步吧。都是呵护你的人，你永远不可能进步。就像一个野生动物一样，它必须要有它的警觉性。<br />
<br />
人物周刊：现有的国内主持人，你比较欣赏谁？<br />
<br />
赵忠祥：这个问题我拒绝回答。<br />
<br />
人物周刊：为什么拒绝？<br />
<br />
赵忠祥：没有理由的拒绝回答。完全是一种本能的拒绝回答。<br />
<br />
人物周刊：用一句话概括下你的现在吧。<br />
<br />
赵忠祥：野鹤闲云。<br />
<br />
人物周刊：有什么寓意？<br />
<br />
赵忠祥：因为现在没人管我，我希望我们电视台的领导还能继续管我，给我定出一些纪律来，许上哪儿，不许上哪儿，到哪儿去请假。<br />
<br />
人物周刊：你现在可以完全不受约束了？<br />
<br />
赵忠祥：不，法律管我，我不会违反法律的。<br />
