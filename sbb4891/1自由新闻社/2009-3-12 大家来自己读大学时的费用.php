&nbsp; &nbsp;&nbsp; &nbsp;从家庭收入来看，我83年工作时工资43.5元，84年转正后52.5元，我现在每月固定工资1900多，加上其它，我现在的月收入应是84年的50倍。<br />
&nbsp; &nbsp;&nbsp; &nbsp;从学生生活来看，记得当年伙食团的菜价大约是：咸菜1分钱，烧白1角伍分，炒瘦肉片2角，小白鲢一条2角伍分。每顿四两粮，每天两顿有肉，每个月那20来块钱够了。前不久我在西南大学当了一段时间的学生，吃食堂，早餐3元，午餐和晚餐各5元，一个月大致得用400多元，加上零用，至少得500元。现在的生活费用是我当年的25倍。<br />
&nbsp; &nbsp;&nbsp; &nbsp;综合上述两个方面来看，当时的1元，可抵现在的30元。如果按这个标准来看，那时国家每个月给我的生活补助相当于现在的600多元。<br />
&nbsp; &nbsp;&nbsp; &nbsp;我女儿现在某政法大学读书，每年学费4500元，住宿费1200元，书费等500元，共交学校6200元。另，每个月给800元生活费，一年总费用至少15000。最穷的家庭，全部平均起来月供恐怕不能少于1千吧，即年供1.2万。<br />
&nbsp; &nbsp;&nbsp; &nbsp;我按现在的物价水平和学校收费标准，来算当年国家给我补助了多少钱：<br />
&nbsp; &nbsp; 学费、书费、住宿费等，6200×4＝24800，<br />
&nbsp; &nbsp; 生活费，600元/月×10月/年×4年＝24000。<br />
&nbsp; &nbsp; 两项合计：4.88万。平均一年12200元。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; <strong>家里在四年中给我的钱（记不太清了，往多里说）</strong><br />
