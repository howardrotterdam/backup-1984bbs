位于天河珠江新城繁华地段的冼村，是广州著名城中村，历史悠久。前晚深夜至昨日凌晨，天河区出动防暴警察、保安及城管等逾千人到冼村清拆现场戒备，约2,000冼村居民到场敲锣打鼓抗议，有村民高喊「你O逼到我O无屋住」、「誓死保护家园」等口号。<br />
