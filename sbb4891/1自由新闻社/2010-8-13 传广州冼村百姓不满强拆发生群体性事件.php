																											<div id="postmessage_781093" class="t_msgfont">天河冼村拆迁改造亚运前完成 地块被保利相中<br />
Posted:2009-8-12 8:35:49<br />
楼市快讯 发表于 搜房网 - 广州业主论坛 - 楼市快讯<br />
天河区冼村改造正式摆上日程，计划要在2010年亚运前完成整村拆迁改造。近日，天河区实施“宜居”的城市发展战略，其中一步提到要启动亚运城市行动计划，积极推进“城中村”改造，位于珠江新城的冼村和位于奥体中心附近的新塘村，已有计划要于2010年亚运前完成改造拆迁任务。据透露，相关改造方案正待审批，但相关政府部门工作人员已在着手为改造做准备工作。<br />
<br />
