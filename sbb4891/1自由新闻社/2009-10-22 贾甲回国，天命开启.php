<br />
&quot;做&quot;是成功的秘诀：<br />
不做任何人也帮不了你，不做永远不会有出息，一切都无从谈起。只要做就能提高层次和价值，只要做共产党就会&quot;重视&quot;你，只有做全社会才会关注你。不做就&quot;白来一世&quot;，后悔莫及！<br />
