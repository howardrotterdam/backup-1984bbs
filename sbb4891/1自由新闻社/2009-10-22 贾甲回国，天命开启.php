有强敌和对手才是个完整的事物，强敌和对手能够有效的推动我们的进步和成功。对手和强敌是一面大镜子，可以使我们直观看到自己的不足和过失，有错即改，一分不拖。我们要学习和借鉴别人的失与得竭尽全力少犯错误，但知错不改是&quot;犯罪&quot;。要在&quot;做中改&quot;，不能停下，直至共产党灭亡！<br />
