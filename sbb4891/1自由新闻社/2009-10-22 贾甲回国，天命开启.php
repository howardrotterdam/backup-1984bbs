要学会在孤独状态下进行运&quot;做&quot;：<br />
在推翻共产暴政实现中国民主的运作过程中，我们要学会开发和创新，用科技攻关的方式来战胜共产党。要走在时代的前头做示范，不要做&quot;后台老板&quot;。不要怕孤独，因为前沿、先知或神圣的东西永远在少数人的手里所掌握。不要害怕被所有人反对或误解，要学会做表率，勇作开拓者和敢于独闯天下，一个人战胜共产党！只要我们使用突然出击的方式，一个人或几个人就能推翻共产党政权。<br />
