要做就直截了当，动真格的。不能似做非做、前怕狼后怕虎、欺软怕硬、摸棱两可。要做就大胆公开专业化；要做就对准共产党的要害部门和一把手；要做就去推翻共产党政权和实现中国的民主。不要害怕，中国人的所有恐惧，都是自己吓唬自己。只要你做的&quot;准&quot;，7800万党员干部和全国人民就是你的合作者和后盾！<br />
