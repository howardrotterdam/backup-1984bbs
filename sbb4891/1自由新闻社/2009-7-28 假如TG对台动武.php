																											<div id="postmessage_181691" class="t_msgfont">・王力雄<br />
<br />
对未来中国的命运，台湾是一个异乎寻常的因素。虽然两岸实力相差悬殊，台湾却可以成为大陆自身危机爆发的诱因。<br />
目前中共对台湾采取经济优惠、政治打压的策略，与其在大陆实行的经济主义路线一脉相承。这种策略使台湾经济与大陆经济的融合度日益提高，似乎两岸经济交织在一起，台湾就不能再搞独立。但是前苏联各国经济依赖岂非更紧，又何尝避免了分裂？民族（族群）问题从来不是经济问题，首先是政治和人文的问题。中共在政治上打压台湾的策略，虽可以使台湾独立的空间缩小，但因为造成对台湾人感情和尊严的伤害，导致台湾人（尤其是青年）的心理逆反，台独倾向日益增长。而一个民主社会的长远走向必定是其民心所向，从这点言，可以说台湾独立只是时间问题（除非大陆有重大变化）。<br />
目前台独政治家的策略是通过“公投法”，让公众可以对公共政策进行表决。这是民主政治的合法性所在，也是民主社会的基本人权，没有什么力量可以阻挡。有了公投法，就可以在未来寻找时机启动统独公投。而只要公投结果是多数赞成独立，台独在今日的国际政治框架中就有了基本合法性。<br />
中国政府激烈反对台湾公投立法，是因为清楚台湾的民意会倾向台独。大陆主要靠的是武力恫吓，这对台湾商人和政客有用，却不一定能吓住普通百姓，反而过去的经验是大陆越恫吓，台湾民众越逆反。<br />
一旦台湾以公投选择台独，中共当政者几乎不会有选择空间。一是中共一直靠民族主义弥补意识形态缺失，几十年的宣传把台湾置于焦点，因此战争呼声会在大陆铺天盖地，形成强大民意；二是“统一”作为大陆惟一实质性的“政治正确”，也是权斗场上的武器。凡是希望避免战争的企图都会被当作把柄，引起权力集团和军队内部的攻击，因此中共决策者会惟恐被视为软弱，抢着扳下战争机器开关。<br />
战争结果无外两种，迅速占领或久攻不下。大陆当然会全力争取前者，抢在民主世界（尤其是美国）做出反应前形成胜利事实，逼迫世界认可。然而民主世界是否会接受一个专制政权进攻一个民主社会呢？理由仅仅是民主的人民进行了一次民主表决？在人权高于主权日益成为共识的今日，即使各国政治家出于利益算计不愿和中国闹翻，手握选票的民众也会迫其政府对中国进行制裁。今日中国和June Fourth时的中国已大不一样，仅出口和投资两项，只要民主国家对中国关闭大门，中国经济就会陷入危机，其他危机也会被随之引发。<br />
也许中共当政者希望靠时间让世界接受占领台湾的现实，如同June Fourth那样以“淡忘”而让世界最终放弃制裁。然而大陆当局能让June Fourth后的中国鸦雀无声，却不能让被占领的台湾鸦雀无声。占领只是第一步，它如何管理2300万有过民主权利的人民呢？实行它许诺的一国两制？台湾人就有借助“两制”进行反抗的空间。大开杀戒？那会有众多义勇军与它战斗不已。而每天面对这些反抗和战斗的“电视连续剧”，世界又怎么会“淡忘”台湾？只能越来越强烈地受到刺激。<br />
如果战争结果是久攻不下，麻烦就更大，因为以大打小，不赢就是输。中国的“愤青”会兴起铺天盖地的咒骂，权力集团和军队内部觊觎高位的少壮派也会掀起讨伐，引起内部权斗的政治危机；拖延的战争会动摇人们对中国经济的信心，而现代经济的命脉就是信心。特别是中国经济重心的东南沿海，上海、广州和香港都在台湾导弹射程内。几枚导弹就可能引起外资撤退、股市崩盘、银行挤兑等后果，造成整个中国的经济大厦随之崩塌。<br />
战争拖延还能使国际社会有反应时间，那些反应必定不会有利于中国。除了经济制裁，军事干预也非没有可能。尽管美国政府眼下反对台湾独立，但并非意味它到时不会出兵援台。美国政府不愿招惹麻烦，然而美国精神却不会允许其坐视。那种美国精神植根于美国选民对政府的约束，也体现于对全球推行美国价值观的战略。决心建设世界新秩序的美国如果任凭中国对台动武，其何以面对世界，又何以扮演满足美国人豪情的全球领袖？而若美国卷入台海战争，战争的结局也就事先可知。<br />
战争对哪一方，包括美国和世界都不会是好事。但即使各方心里都明白，也只能眼瞅事情按如此轨道发展。因为这是一个“局”，各方的互动逻辑已定，结果也就别无选择。而在在这个“局”中，损失最大的只能是中国大陆。这就看出专制制度何以是祸。大陆虽有绝对的实力优势，台湾最好的武器却是民主制度。只要战争一开，民主世界会立判正义邪恶，黑白分明地站到台湾一边。因此，大陆攻打台湾绝非是一场没有悬念的战争，反而大陆很可能是不战已输。我相信中共高层也明白这一点。在台湾为大陆动武惴惴不安的时候，中共高层也在惴惴不安，大陆又何尝敢打台湾呢？</div>

							
							
							
							
