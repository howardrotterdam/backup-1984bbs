<br />
　　与彭长健同日被“双规”的，还有重庆市北碚区的副区长赵文锐、重庆市检察院第一分院副检察长毛建平，三人均为副厅级干部。毛建平被指在一些案件中袒护黑恶势力而被掀翻，赵文锐亦据称包庇纵容黑社会老大王兴强。<br />
<br />
　　实际上，赵文锐落马前，北碚区公安分局原局长谢德玖、原副局长王小恒、朝阳派出所原所长林海等人因与黑老大王兴强勾结已被刑拘，副局长赵新生亦被控制接受调查。<br />
<br />
　　王兴强是北碚黑社会第一号人物，该人最早是北碚杀猪场一名收廉价猪下水的小商贩，后来发展成用黑恶势力控制垄断该区猪肉市场，成为“猪霸”，使得北碚区市民近十年来吃的猪肉比主城各区每斤要贵八毛到一元多。<br />
<br />
　　谢德玖2006年被任命为重庆市公安局北碚区分局局长，王小恒是其分管刑侦的副手。据内部人士透露，两人倒掉是因为充当王兴强的保护伞，并与其勾结，一起从事黄赌毒生意，还强行入股修路、基础建设等赢利行业。<br />
<br />
　　一批由谢德玖、王小恒、王兴强等人合股的娱乐城和赌场，近年来在北碚相继开张。整个北碚的猪肉、娱乐场酒水、渣场也被他们控制，知情者称仅娱乐场酒水每天进账就高达18万元。<br />
<br />
　　知情者向时代周报记者透露，王小恒妻子只是北碚区公安分局一名普通民警，每天竟然驾驶“路虎”上班。<br />
<br />
　　2008年底，谢德玖和王小恒分别调任重庆市公安局公交治安管理分局局长、渝北公安分局副局长。这次打黑风暴中王兴强的被抓，谢、王两人被牵出。接近专案组的人士透露说，警方在搜查王小恒家时，一捅开天花板，竟哗啦啦掉出了数百万现金，因无法解释来源，王小恒之妻跳楼自杀。<br />
<br />
　　此前的6月26日下午，北碚另一位绰号“山猪”的黑老大在闹市被人当街杀死，王小恒闻讯后穿着警服亲自前往吊唁；几天后死者的出殡车队有50多辆顶级轿车，浩浩荡荡从城市中心穿过。而此时，重庆全面打黑行动启动已半月。<br />
<br />
　　重庆打黑风暴，已引发了重庆警方官场的地震。<br />
<br />
　　在众多重庆市区县公安部门官员看来，最具震慑力的莫过于8月18日。这一天，重庆市公安局召开的打黑除恶警示教育会。<br />
<br />
　　在这个会上，垫江县政府副县长兼县公安局局长徐强、重庆市公安局经侦总队队长陈光明等3位处级官员被扯掉警徽，当场带走。一名知情人士向媒体透露称，警方的行动迅速而突然，在场的多位区县公安部门负责人眼看着同僚在开会过程中被带走，“不禁噤若寒蝉”。<br />
<br />
　　徐强8月13日还亲自下乡摸底，布置工作，带队在当地周家镇打掉了一个黑恶势力团伙。这次突然被“双规”，据称是因为徐强被查出，在九龙坡区公安分局任副局长期间，庇护当地黑恶团体，并对这些黑恶势力实行按效益“收税”，明码实价、一视同仁。<br />
<br />
　　而身为重庆市公安局经侦总队总队长的陈光明，在全国警界赫赫有名，曾是全国省(市)级公安禁毒战线上唯一的女总队长，十七大代表，全国十大女杰出青年，全国劳模。“下辈子还要当警察！”这是陈光明曾说过的一句誓言。<br />
<br />
　　陈光明为何被双规，目前尚无权威说法。但重庆警界内部人士向时代周报记者说，陈光明的落马可能是受文强案牵连，“她和文强的关系非同一般”。一宣称和文强相识20多年的人士甚至向时代周报记者称，她和文强是情人关系。<br />
<br />
　　越来越多的警察在这场打黑风暴中“陷落”。9月8日，时代周报记者从重庆警方内部得到准确信息：就在当天下午，市刑警总队一副支队长被突然带走，此前该总队一副总队、一支队长已被控制。<br />
<br />
　　据警方内部人士的最新统计，今年打黑风暴掀起以来，重庆公安系统处级以上官员落马的已突破30人，被处理的警员攀升至近200人。<br />
<br />
　　与此同时，重庆法院、检察院系统也发生“强烈地震”，继重庆市高级人民法院副院长张|、原执行局局长乌小青等人被控制后，已有大批官员被掀翻落马。重庆市检察院最近宣称，深挖严打黑恶势力“保护伞”将作为今后全市检察机关打击黑恶势力犯罪工作的重中之重。<br />
<br />
　　最大的悬念<br />
<br />
　　重庆的打黑风暴，已经吸引了境内外媒体的一致关注。但重庆官方却对打黑的新闻发布非常谨慎。8月中旬，重庆市公安局只通过当地媒体发布了一次信息。众多官员因涉黑落马的消息，几乎都最先来自网络，各路媒体记者经多方艰难核实方证实，几乎不见重庆官方回应。对此，重庆官方的解释是，目前打黑进入 “深水区”，“现在是非常敏感和关键的时期，只能低调”。<br />
<br />
　　警方内部消息说，早在打黑风暴掀起时，重庆市公安局“打黑”专案组的核心办案人员均被要求签订了一份保密协议，不仅本人不能对外发布一切关于打黑斗争调查的信息，连家人也被要求“封口”。<br />
<br />
　　“领导不说话，我们也不敢公开消息。”重庆市公安局刑警总队一位警察向时代周报记者表示，领导每天在大会小会上强调的都是纪律，“即使是同事被“双规”、刑拘，领导不主动传达情况，任何人不得打听。”<br />
<br />
　　“不过问，不打听，不传言”―这是重庆警方的内部戒律。<br />
<br />
　　以上警察坦言，由于信息的封闭，整个单位的气氛更加神秘、紧张，很多人晚上睡不好觉，总担心什么时候“厄运”就会降落自己头上。“打黑启动以来，我们的工作强度非常大，开会开到晚上11点多是常事。”这位警察说。<br />
<br />
　　据重庆警方传出的最新消息，一位47岁的打黑专案组民警，曾连续工作21天，每天吃住在专案组。就在半个月前，这名警察因操劳过度，在提审涉黑犯罪嫌疑人时，突发脑溢血晕倒在了工作岗位上，经医院全力抢救方脱离生命危险。<br />
<br />
　　各种迹象表明，重庆打黑行动已进入全面攻坚阶段。据当地媒体报道称，9月8日，重庆市政法委书记刘光磊一行代表市委书记薄熙来慰问了一线打黑干警，表示“要将打黑除恶进行到底，绝不给黑恶势力翻身机会”，“做好打持久战的准备”。<br />
<br />
　　“重庆打黑，下一步将采取怎样的战略和战术？”重庆警方至今尚无明确说法。重庆市公安局相关负责人9月8日表示，重庆的打黑专案组由最初的14个发展到了目前的200个，参战警员由3000人增加到了7000人。<br />
<br />
　　一些观察人士分析认为：“这么多的警力投入，意味着目前重庆警方对早就浮出水面的大的黑恶势力团伙的打击，可能基本告一段落，下一步打黑主战场将由主城推向远郊区县，打黑行动由点向面上铺开。”<br />
<br />
　　“客观地说，重庆掀起的打黑风暴，犹如撕开了一道社会伤口，让我们得以看到黑社会问题的严峻性，与官僚阶层、经济部门交叉感染的复杂性，其盘根错节已触及到地方行政资源、经济资源的重组。”重庆警界内部人士坦承，处于风口浪尖、外界瞩目的重庆打黑，到目前已经很难停下来了。<br />
<br />
　　重庆打黑能走多远？这或许正在考验着重庆这座西部大城的决策者的政治智慧和眼光。而对广大关注者来说，重庆这场声势浩大的打黑风暴，目前留给大家最大的悬念就是：黑社会“保护伞”将打到哪个层级？是否设置有级别限制？<br />
<br />
