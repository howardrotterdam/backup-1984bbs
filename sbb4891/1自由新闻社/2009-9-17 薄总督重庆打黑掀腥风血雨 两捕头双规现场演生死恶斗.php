													<h2>这篇很有演义味道――</h2>
						
						
																											<div id="postmessage_245846" class="t_msgfont">重庆打黑进入深水区 公安局副局长彭长健落马<br />
<br />
2009年09月10日21:11<br />
来源：时代周报 作者：邓全伦<br />
<br />
<br />
　　一切来得太突然。<br />
<br />
　　“重庆市公安局副局长彭长健涉黑落马”―9月4日中午，重庆市公安局民警陆松(化名)在网上看到这条消息时震惊不已。<br />
<br />
　　几个小时前，彭长健还在局里举行的一个会议上，向全局干警部署了相关工作。这个会议未完，彭长健就赶往市委办公厅开会。会议正在进行当中，他即被重庆市纪委的人当场带离会场予以“双规”。<br />
<br />
　　此时，彭长健在市局会议上的讲话刚经重庆市公安局相关处室整理打印出来，正准备以文件形式下发。<br />
<br />
　　这是重庆打黑风暴中，继该市公安局前常务副局长文强落马后，又一名市局高层因涉黑而倒掉。据重庆警方内部知情人士统计，截至目前，重庆市公安系统因涉黑被掀翻落马的处级以上官员已突破30人，警员高达200多人。<br />
<br />
　　然而，这场风暴并没有结束的迹象，已然走向纵深，进入“深水区”。一方面，警界人人自危；一方面，百姓众声叫好，但心中仍有巨大的悬念待解―“重庆打黑能走多远？”<br />
<br />
　　“都市猎手”被猎<br />
<br />
　　彭长健是继文强后，重庆警界落马的第二个厅级官员。在许多人看来，彭长健的落马并无前兆，在这次打黑风暴中，他是重庆市打黑除恶指挥部成员之一。<br />
<br />
　　然而时代周报记者调查发现，早在8月8日，搜房重庆BBS上，就有网友在“重庆市司法局局长文强涉嫌严重违纪，正接受调查”的帖子后，跟帖直称：“还有公安局的彭长健也是个黑社会”，“黑社会的彭长健，×××，你们的日子也不远了！”<br />
<br />
　　知情者向时代周报记者透露，彭长健今年46岁，出身工人，最早是重庆市餐饮服务公司一名负责白案的厨师。上世纪80年代公招进入重庆市中区(现渝中区)公安分局担任普通民警，由于工作出色，此后升任秘书科科长、解放碑派出所所长；1997年5月被任命为渝中区公安分局副局长，分管刑警支队、经文保科等部门的工作。<br />
<br />
　　“他善于学习、肯钻研，工作业务能力强，多次立功。”以上知情人士称，彭长健形象好，一度成为渝中区公安系统女警崇拜的偶像。2000年11月，彭长健坐上了渝中区公安分局局长位子，并担任渝中区常委兼政法委书记。<br />
<br />
　　2004年出版的一本《神州》杂志，其中一篇专门写彭长健的文章称其为“都市‘猎手’”―“都市里伟大的保护神”。该文称彭长健上任渝中区分局局长后，积极改变装备落后状态，装备了一大批高科技警械；他长期关心民警的健康，2004年在全市区级公安机关里率先为全体民警购买安康险。<br />
<br />
　　在以上知情者印象中，彭长健为人强硬、霸气，曾留大背头、八字须。彭的这种性格，得到了重庆市璧山县公安局有关人士的证实。璧山县公安局是彭调任重庆市公安局副局长后的工作联系点，但他每次到该局检查工作都会训斥人，且毫不留情面，这让很多干警对他心生畏惧。<br />
<br />
　　“彭长健和文强性格相投，关系密切。”重庆警界人士称。一种说法是，在抓捕悍匪张君的过程中，彭长健因为踏实能干得到了市公安局副局长文强的赏识。<br />
<br />
　　2005年12月15日，经重庆市政府第64次常务会议决定：任命彭长健为重庆市公安局副局长。2006年他兼任政治部主任和党委委员，分管干部人事工作，位居重庆市公安局第四把手。<br />
<br />
　　据接近重庆警方的人士透露，2008年6月，王立军空降重庆，接替文强担任公安局常务副局长后，一度遭到彭长健的抵制，“害得王的工作难以贯彻下去”，王立军对他很不满意。<br />
<br />
