																											<div id="postmessage_527868" class="t_msgfont">大米再次从北韩人民的餐桌上消失. 饥荒再度降临, 可能会像90年代那样, 上百万人被饿死.<br />
<br />
那些有勇气穿越带刺铁丝网和巡逻守卫溜到中国的人, 脸上刻满了绝望. 与其说寻找自由,他们来到这里只是为了填饱肚子.<br />
<br />
&lt;Times&gt;报在位于中国的一间房子里采访了4位最近刚穿越边境来到这的妇女. 她们描述了在日益临近的饥荒威胁之下, 现在北韩境内的绝望境况. 她们当中最年轻的一位只有16岁, 上个月刚从冰冻的河面逃过来. 其她三位则都是50多岁, 去年就逃过来了, 并且闭口不谈她们是如何逃过来的: 因为她们还要回去帮助她们的家人.<br />
<br />
外面下着雪, 在这间离边境不远的不知名的公寓里, CHOI KUM OK蹲在地上. 当提到她还在北韩的儿子, 她眼中充满泪水: “我来这挣钱就是为了给他治病凑医药费. 我必须给他送吃的过去, 否则他就要挨饿了.”<br />
<br />
当回忆到90年代, 粮食歉收而造成十分之一的人挨饿时, 她捂着脸抽泣着. 她说, “我不想说那时候的事”. 90年代的饥荒令她失去了一位至亲.<br />
<br />
身为劳动党党员以及做过保卫工作的她, 无法理解她仍旧崇拜的领袖们会如此彻底地背弃他们的人民.<br />
<br />
因为平壤当局下令射杀逃北者以及中国方面已经对不断到来的难民失去耐心,来自朝鲜的难民潮近年来萎缩成了三三两两的小规模逃难. <br />
<br />
极少数人虽过关斩将成功结束了这段危险的旅程, 却生活在持续不断的恐惧之中. 他们中多数打算在中国待上几周或几个月, 有了点积蓄之后就带着钱回家. 少数想去韩国.<br />
<br />
他们确信饥荒的到来是早晚的事, 因此冒险越境来逃难. 他们面临的饥荒实际上是因为”青黄不接”, 或者说是4月左右, 地里的麦子还没熟, 而存粮已经不多了. 有人说:” 这个时候是最有可能开始爆发饥荒的.”<br />
<br />
有些人已经因营养不良而生命垂危, 这位妇女说. 过去一段时间虽然吃的不是很充足, 但一直持续供应, 因为90年代饥荒之后政府放松了对自由市场的禁令.<br />
<br />
但是在去年11月, 政府采取了发行新币兑换旧币的政策(这段政策大家都知道就不翻了). 上周, 负责此次货币改革的掌管财政的部长被枪决.<br />
<br />
CHOI女士说:” 那天中午我们才听说换钱的事, 所以我们只好5小时内赶在银行关门之前去换钱. 好多人因为没换钱倾家荡产.<br />
<br />
黑市关掉之后, 食品就没再流通. 商人们没动力去卖那些现在已经是无价的东西, 而是囤积米和油来应对他们要面临的饥荒. 为了发泄失望和不满, 许多人把攒下的钱通通扔掉<br />
<br />
16岁的圆脸女孩SongHee说: “ 有些人把钱扔进河里. 甚至还听说一个人把他的钱给烧了,钱上面有金日成的头像, 所以说这就像你在烧伟大领袖―这是犯罪. 那个人被处决了. 这件事是真的, 就在清津港.”<br />
<br />
无论真伪, 这传言反映出人们前所未有的对政权的不满: 一个西方世界正在想法防备的, 拥有核武器的流氓政权<br />
