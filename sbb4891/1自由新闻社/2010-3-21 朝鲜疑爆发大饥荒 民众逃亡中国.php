泰晤士报：朝鲜疑爆发大饥荒 「已有人饿死」民众逃亡中国<br />
【明报专讯】朝鲜疑再爆发大饥荒，《泰晤士报》记者访问了4名从朝鲜逃到中国的妇女，她们表示，朝鲜已有人死于新的饥荒，她们为令孩子和家人有食物，才被迫犯险逃到中国。一旦情?发展至1990年代般的大饥荒，将可能有多达数百万人死亡。<br />
