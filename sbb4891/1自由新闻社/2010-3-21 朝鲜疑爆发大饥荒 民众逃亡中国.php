																											<div id="postmessage_527687" class="t_msgfont">Once again, rice has disappeared from tables in North Korea. A famine looms and ― as happened in the 1990s ― millions could die.<br />
<br />
Desperation is stamped on the faces of those few who have braved barbed-wire fences, armed guards and patrols to slip into neighbouring China. They seek food over freedom.<br />
<br />
The Times met four women in a safe house in China this week who fled recently across the frontier. They described despair in North Korea at the growing prospect of starvation in the Stalinist state. The youngest, only 16, crossed the frozen river last month. The other three, in their 50s, left last year and were tight-lipped about how they got out because they must go back to help the families they left behind.<br />
<br />
While snow falls outside, Choi Kum Ok squats on the floor of an anonymous apartment not far from the border. Her eyes fill with tears as she talks of the son she had to leave behind. “I came over to earn money for his medical care. I need to get him food or he will starve.” <br />
<br />
She covers her face and sobs as she remembers the 1990s, when harvests failed and up to 10 per cent of the population starved. She lost a sibling. “I don’t want to talk about it,” she says.<br />
<br />
A former security guard and member of the elite ruling Workers' Party, she cannot understand how the leaders that she still worships could have failed their people so completely.<br />
<br />
The flow of refugees from North Korea has slowed to a trickle in recent years, as Pyongyang has issued shoot-to-kill orders to guards, and China has lost patience with the arrivals. Beijing ― nervous about instability across its border ― props up the nuclear-armed regime with oil and food.<br />
<br />
The few who have made the dangerous journey live in constant fear of discovery. Most plan to spend a few weeks or months in China to build up savings to take home. A few want to go to South Korea.<br />
<br />
They risked the crossing to survive the famine they believe is now imminent. The crisis they face now is the “barley hump”, or the barren period around April when rice stocks run out and barley has yet to be harvested. One said: “This is the most dangerous period. This is when I’m afraid we will start to see people begin to starve.”<br />
<br />
Some are already dying from malnutrition, the women said. Food had been available, if not plentiful, since the Government relaxed the ban on free markets after the 1990s famine.<br />
<br />
In late November, however, the Government abolished old banknotes and introduced a new currency at the rate of 100 old won to one new. The maximum people could change was 100,000 won. Private savings, such as they were, were wiped out. The North Korean minister held responsible for the reform was executed by firing squad last week.<br />
<br />
Mrs Choi said: “I heard about the currency decision at midday. So we had only until the banks closed about five hours later to change our money. A lot of people lost everything.”<br />
<br />
Food disappeared overnight as the black markets closed. Traders had no incentive to sell now-worthless products; better to hoard their rice and oil for the times of need they knew lay ahead. Venting their frustrations, many people with savings threw them away.<br />
<br />
Song Hee, a round-faced 16-year-old, said: “Some tossed the money into the river. I even heard one man burnt his notes. The money has the face of Kim Il Sung on it, so it’s like you’re burning the Great Leader ― and that’s a crime. The man was executed. Really, it’s a true story. It happened in Chongjin city.”<br />
<br />
True or apocryphal, the rumour reflects an unprecedented sense of dissatisfaction with the leadership of a rogue nuclear power that the West is trying to corral.<br />
<br />
In a country where obedience equals survival and where Kim Il Sung, the late Great Leader, and Kim Jong Il, his son and successor, the Dear Leader, are revered as divine, opposition is almost unheard of. The currency reform, however, was deeply unpopular. One woman said: “People complained. It’s not like it was. Everyone has an opinion.”<br />
<br />
Such grumbling is voiced only among those who trust one another. Song Hee said: “If people hear you, then you get sent to prison.”<br />
<br />
Jeong Hee Ok says she finds it difficult to believe government pledges that reforms will succeed by 2012 ― the centennial of the birth of the Great Leader.<br />
<br />
“I am able to eat three times a day in China and I think about my daughters every time I eat.”<br />
<br />
Li Mi Hee, 56, waded through a freezing river to China, where she cares for an elderly man and makes 500 yuan (&#163;50) a month ― the equivalent of 10,000 won.<br />
<br />
“My son tells me people are already dying of hunger again,&quot; she says. &quot;In the 1990s I would see dead bodies lying in the streets and now this could happen again.”<br />
<br />
She sends back anything she can. “I hear from my son. He tells me he has no food. He will starve. I have to do something.”<br />
<br />
The World Food Programme says that the food situation is getting more acute. A spokesman said: “With even more food shortages, the situation could deteriorate even further.”<br />
<br />
Those who have left are torn between an enduring belief in the omnipotence of their leaders and despair at their poverty and hunger. Jeong Hee Ok was shocked by criticisms of Kim Jong Il in China. “People curse him. It’s so upsetting. Since I was a child I learnt he was the kindest and the best person.”<br />
<br />
Before they left, all had attended political sessions at which they learnt that Kim Jong Il was to have a successor: his 26-year-old son, Kim Jong Un. Jeong Hee Ok said: “He is very young, not even 30, and very intelligent. We are happy because he will bring new ideas.”<br />
<br />
To celebrate the birthday of the heir-apparent, extra food rations were distributed on January 18 ― even rice and a little oil. Jeong Hee Ok plans to slip back after making money to pay for her daughter’s wedding.<br />
<br />
“My country is good. Here in China if you want a child you have to pay, but we can have children for free.”<br />
<br />
Mrs Choi says everyone she knows believes in the leadership. But then her eyes slide away. “They believe because they don’t know what it’s like outside. No one tells them. The younger people know more.”<br />
<br />
Li Mi Hee is the least afraid, perhaps because her eldest son died in a labour camp. “China is a great place to live. I never want to go back. When North Koreans can live like China, that will be so good. People are complaining. Before they were scared, but things have changed since the 1990s when so many people died and said nothing.<br />
