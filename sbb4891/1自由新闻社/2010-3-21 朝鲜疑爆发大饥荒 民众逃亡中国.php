最年轻的16岁受访者于上月冒死跨境到达中国，另外3名50多岁的妇人则于去年来到中国。他们对如何从朝鲜来到中国守口如瓶，因她们仍希望回到朝鲜帮助家人。当谈到她留在家中的孩子时，其中一名受访者崔金玉（音译）立即热泪盈眶：「我来这里为他的医疗开支挣钱。我需要为他买食物，不是的话他会饿死。」<br />
