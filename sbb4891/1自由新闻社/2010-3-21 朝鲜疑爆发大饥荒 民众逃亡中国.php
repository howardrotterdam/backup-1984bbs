<br />
报道称，不少朝鲜人都犯险越境去逃避他们预料可能即将发生的饥荒，计划在中国挣钱几个星期或几个月后回国。他们都表示，4月是朝鲜饥荒的高危日子，因为这个时期田中的米仍未能收割，但储备的米已开始耗得七七八八，饥荒就很可能在4月出现。其中一人说：「这是最危险的时期。我担心人们开始在这时候饿死。」她们均表示，国内有人已经饿死。<br />
<br />
在朝鲜于1990年代发生大规模饥荒以来，政府就开始放宽市场禁制，尽管食物不多但一直都有供应。但在去年11月，政府突然推行货币改革，旧钞票停用，强迫人民把100元旧钞兑换成1元新钞，上限为10万朝鲜元，令不少人的毕生积蓄付之流水。负责改革的前计划财政部长盛传已于上周被当局枪毙。<br />
