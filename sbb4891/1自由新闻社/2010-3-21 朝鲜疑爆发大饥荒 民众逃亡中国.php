在这个国家, 生存等价于服从, 伟大领袖金日成和他儿子, 敬爱的领袖金正日, 被奉若神明. 几乎无法听到反对的声音. 然而, 这次货币改革却大失民心. 一位妇女说: “ 人们都在抱怨(这项政策). 不像以前了, 现在大家都有自己的想法.”<br />
