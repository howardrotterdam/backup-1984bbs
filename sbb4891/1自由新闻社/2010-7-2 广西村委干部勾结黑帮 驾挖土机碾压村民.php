													<h2>被履带碾压的中国人</h2>
						
						
																											<div id="postmessage_722725" class="t_msgfont">村干部为争施工权勾结黑势力用推土机碾死农村老人<br />
广西78岁老人因村干部争工程被挖掘机司机碾死<br />
<br />
6月26日上午，在广西资源县中峰乡车田湾村朝宝窑自然村，78岁老人王庆柏站在一台挖机前面，让司机程某停止施工。在争执中，程某开动挖机，王庆柏的脚被压到并被当场碾压致死。事发后，程某先是逃离了现场，后又向警方投案自首，被警方刑事拘留。<br />
<br />
78岁老人被挖掘机碾死　<br />
27日上午9时许，记者来到车田湾村朝宝窑自然村。在事发现场，停着一辆挖掘机，旁边还搭着一个简易的灵棚，一口棺材放在里面。10多名戴孝的男女向每一名前来探望的亲友哭诉着。　<br />
王丽梅是朝宝窑村的一名队长，26日惨剧发生时她就在现场，她向记者讲述了事发时的情况。　<br />
王丽梅说：“26日上午9时许，王庆柏儿子的挖机正在施工，这时来了5个人进行阻止。但是程某的挖机却开动起来施工。我、王庆柏和另外两个村民来到程某的挖机前，王庆柏站在挖机履带前和程某等人理论，质问为什么不让他儿子的挖机施工。在理论中，程某开动了挖掘机，王庆柏的脚被履带压着了，我反应过来想扯开王庆柏，可是已经扯不动了。我们赶紧冲着挖机操作室喊‘压着人了，赶紧停下来’，可程某完全不理我们，接着往前开，从王庆柏身上碾压过去，又开了约一米远才停下来。随后，程某从操作室跳下来就跑，我们没能拦住他。王庆柏被碾压后当场就断气了。”　<br />
因争工程发生矛盾　<br />
据死者王庆柏的儿子王某介绍，有外地老板在该村投资建设一家大理石加工厂，他和另外两人合股购买了一台挖机，经外地老板同意，参与平整土地。6月13日，他的挖机要进场施工时，车田湾村村委副主任程鹏（化名）的挖机挡在路上，不给他进场施工。几天后，他另外开了一条路才进到工地。6月21日上午，他的挖机开始施工时，突然有20多人手持砍刀和钢管来到工地，拦住他的挖机不准施工，但程鹏的挖机和程鹏亲戚的挖机仍然还在施工。他当时报了警，那伙来威胁他的人在民警到来前跑了。当天下午，中峰乡go-vern-ment组织他跟程鹏一起协调，乡领导曾当场表态说，双方都可以进场施工。　<br />
王某说，6月26日上午，他的司机开着挖机施工时，又来了五六个人阻止施工，还拿石头砸挖机，他们就停了下来，但是程鹏和他亲戚的挖机仍在施工，“我父亲于是去质问程鹏的外甥程某，结果被程某开挖机碾死”。　<br />
记者采访此事时，程鹏说，6月13日，由于工地还没有施工，他的挖机停在路上，这才挡住了王某的挖机；6月21日，有人拿刀去工地，他当时没在现场，也不清楚情况。对于6月26日上午王庆柏死亡一事，程鹏认为这是一起意外事故。　<br />
挖机司机被刑拘　<br />
资源县公共安全专家局一位姓石的副局长向记者介绍说，案发后，县公共安全专家局十分重视，民警对现场进行了仔细勘查，并进行尸检。当天，犯罪嫌疑人程某来到刑警队投案自首，目前已被警方刑事拘留。警方正对此案进行深入调查，不管案件牵涉到什么人，警方都会实事求是，秉公执法。　<br />
6月27日上午，资源县go-vern-ment办、资源县政法委和中峰乡go-vern-ment相关负责人来到朝宝窑村，做死者家属的安抚工作。同时叫肇事挖机的一名股东与死者家属就善后事宜进行协商。 （记者邓振福 唐晓燕）　<br />
<img src="http://upload3.mop.com/upload3/2010/07/05/02/1278309630241.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
2010年6月26日9时30分许，资源县政府在中峰乡车田湾村委所属朝宝窑自然村筹建的大理石厂工地，一台挖土机司机故意用履带轮碾压一位老人，当场死亡情景惨烈，手段残忍，民愤极大，制造了一起有计划有预谋的故意杀人案件。<br />
2010年上半年资源县政府引进了外省一私有企业拟在车田湾村委所属的朝宝窑自然村土地上筹建大理石加工厂征地工作尚未完成，并未与村民签订任何合同与协议，部分款项尚未到达村民账户，道路尚未开通（只利用原来村民自行开通的机耕路）就进行厂址的土方工程。车田湾村委会付主任程玉红利用付主任的身份组织两台挖土机进场施工，本村王祥通过乡政府同意也经过厂方同意自筹资金组织一台挖土机进场施工（双方都未签订任何合同和协议）。6月21日施工之初，程玉红不允许王祥的挖土机进场，并带了20几个社会闲散人员和两劳人员，手持砍刀和棍棒到场威胁阻拦王祥的挖土机施工，无奈之余报告了乡政府、派出所和有关部门，政府书记要求双方共同施工，但程玉红自恃有靠山有背景，也有个别乡干部的支持（此乡干部在另一工程有过股份），仍不准王祥方施工，乡政府也未采取具体措施，特别是报告人大某主任时，不予受理。<br />
<br />
6月26日上午，程玉红又招来6名两劳人员和闲散人员手持砍刀和棍棒威胁村民，双方在工地发生争执，但未发生肢体语言，王祥的父亲为制止别人伤害儿子到工地进行规劝，不料程玉红方已有杀人之心，挖土机司机程凯直接实施杀人，开机碾压78岁的老人王庆柏，使一条活生生的生命就止终结，程凯弃车逃跑。在场的几个目击证人无不愤慨和悲伤。<br />
