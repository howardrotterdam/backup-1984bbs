													<h2>广西七旬老人被挖掘机碾死 嫌犯已提请批捕</h2>
						
						
																											<div id="postmessage_723954" class="t_msgfont">　　广西78岁老人因争工程被挖掘机司机碾死<br />
　　6月26日上午，在（广西）资源县中峰乡车田湾村朝宝窑自然村，78岁老人王庆柏站在一台挖机前面，让司机程某停止施工。在争执中，程某开动挖机，王庆柏的脚被压到。旁观的人连声劝阻程某赶快停下来，但是，挖机仍然向前开了过去，王庆柏被当场碾压致死。事发后，程某先是逃离了现场，后又向警方投案自首，被警方刑事拘留。据了解，双方因争抢施工工程发生矛盾。<br />
<br />
　　78岁老人被挖掘机碾死<br />
<br />
　　27日上午9时许，记者来到车田湾村朝宝窑自然村。在事发现场，停着一辆挖掘机，旁边还搭着一个简易的灵棚，一口棺材放在里面。10多名戴孝的男女向每一名前来探望的亲友哭诉着。<br />
　　王丽梅是朝宝窑村的一名队长，26日惨剧发生时她就在现场，她向记者讲述了事发时的情况。<br />
　　王丽梅说：“26日上午9时许，王庆柏儿子的挖机正在施工，这时来了5个人进行阻止。但是程某的挖机却开动起来施工。我、王庆柏和另外两个村民来到程某的挖机前，王庆柏站在挖机履带前和程某等人理论，质问为什么不让他儿子的挖机施工。在理论中，程某开动了挖掘机，王庆柏的脚被履带压着了，我反应过来想扯开王庆柏，可是已经扯不动了。我们赶紧冲着挖机操作室喊‘压着人了，赶紧停下来’，可程某完全不理我们，接着往前开，从王庆柏身上碾压过去，又开了约一米远才停下来。随后，程某从操作室跳下来就跑，我们没能拦住他。王庆柏被碾压后当场就断气了。”<br />
<br />
　　因争工程发生矛盾<br />
<br />
　　据死者王庆柏的儿子王某介绍，有外地老板在该村投资建设一家大理石加工厂，他和另外两人合股购买了一台挖机，经外地老板同意，参与平整土地。6月13日，他的挖机要进场施工时，车田湾村村委副主任程鹏（化名）的挖机挡在路上，不给他进场施工。几天后，他另外开了一条路才进到工地。6月21日上午，他的挖机开始施工时，突然有20多人手持砍刀和钢管来到工地，拦住他的挖机不准施工，但程鹏的挖机和程鹏亲戚的挖机仍然还在施工。他当时报了警，那伙来威胁他的人在民警到来前跑了。当天下午，中峰乡政府组织他跟程鹏一起协调，乡领导曾当场表态说，双方都可以进场施工。<br />
　　王某说，6月26日上午，他的司机开着挖机施工时，又来了五六个人阻止施工，还拿石头砸挖机，他们就停了下来，但是程鹏和他亲戚的挖机仍在施工，“我父亲于是去质问程鹏的外甥程某，结果被程某开挖机碾死”。<br />
　　记者采访此事时，程鹏说，6月13日，由于工地还没有施工，他的挖机停在路上，这才挡住了王某的挖机；6月21日，有人拿刀去工地，他当时没在现场，也不清楚情况。对于6月26日上午王庆柏死亡一事，程鹏认为这是一起意外事故。<br />
<br />
　　挖机司机被刑拘<br />
<br />
　　资源县公安局一位姓石的副局长向记者介绍说，案发后，县公安局十分重视，民警对现场进行了仔细勘查，并进行尸检。当天，犯罪嫌疑人程某来到刑警队投案自首，目前已被警方刑事拘留。警方正对此案进行深入调查，不管案件牵涉到什么人，警方都会实事求是，秉公执法。</div>

							
							
							
							
