													<h2>《中国新闻周刊》独家关注公盟事件</h2>
						
						
																											<div id="postmessage_197288" class="t_msgfont"><strong>由于众所周知的原因，本文无法提供电子版， 平面版见今天的本期8月10日版&nbsp;&nbsp;大家购买支持一下吧。报道实在不容易。《中国新闻周刊》英勇披露了公盟事件！向《中国新闻周刊》这样的媒体致敬！</strong><br />
<br />
《中国新闻周刊》431期（目录） 40<br />
<br />
40 公益诉讼“抑郁症” <br />
<br />
<a href="http://www.chinanews.com.cn/kong/news/2009/08-06/1807492.shtml" target="_blank">http://www.chinanews.com.cn/kong/news/2009/08-06/1807492.shtml</a><br />
<br />
公益诉讼“抑郁症”<br />
<br />
――发表于《中国新闻周刊》总第431期<br />
<br />
文/熊巧 本刊记者/周华蕾<br />
<br />
提要：公益诉讼旨在对不平等的资源予以调整，关注着社会中处境最差、最边缘化的群体，而限于当前环境，公益律师们自身更是一个弱势群体。<br />
<br />
 <br />
<br />
坐在许志永对面的两个地税制服一脸苦笑。 <br />
<br />
从5月份查账以来，许志永和这些人已经很熟了。查账过程中许志永一直主动配合，而这些人始终沉默着，私下表示“我们做不了主”。 <br />
<br />
他们身边的一位女科长表情严肃，黎雄兵和彭剑律师正在准备申辩意见，加上主持听证会的税务官白俊，这间大会议室只有7个人，显得空荡荡的。 <br />
<br />
这是7月24日的上午九点，北京地税局410房间正在举行对北京公盟咨询有限责任公司处罚事宜的听证会。由于“涉及国家机密、商业机密或个人隐私”，地税局没有同意公开听证的申请。<br />
<br />
地税局大楼的围墙外面三三两两聚集着律师、媒体和访民。<br />
<br />
北京公盟咨询有限责任公司是一家以法律研究和个案援助为主要内容的非营利组织（简称公盟）。法人代表许志永博士是海淀区第十四届人大代表，北京邮电大学的老师。<br />
<br />
7月14日，公盟同时接到分别来自北京市国税局、北京市地税局下达的税务行政处罚事项告知书，两局认为公盟偷税共计24万元，并同时对它处以5倍的最高额度的罚款，罚款总额为142万多元。<br />
<br />
7月17日，北京民政局做出取缔公盟下属机构“公盟法律研究中心”的决定，并上门查抄，没收包括电脑、办公家具、案件资料、调研报告等在内的所有物品。<br />
<br />
在短短四天之内，公盟受到国税、地税、民政三个部门的执法。<br />
<br />
围墙外的人群也因此而来。 <br />
<br />
7月17日上午9点，20名民政执法大队的人来到公盟位于华杰大厦6A2的办公室。他们扛着摄像机，对惊愕不已的工作人员宣读了《取缔决定书》，然后把他们带到楼下物业，每人一个房间进行问讯，最后让他们 “收拾好私人物品迅速离开”。<br />
<br />
两名志愿者小姑娘当场吓哭了。 <br />
<br />
不久，许志永、滕彪等负责人赶到。“会议室所在的房间被扫荡一空，办公室的全部设施都被抄走，只剩下四张桌子、一个书架。<br />
<br />
我们最心痛的是各种材料：诉讼案卷、上访信、网友来函、会议记录，每一张纸都是凝结了我们的心血。一些上访者只能捡菜叶子吃，却花钱复印自己的冤案材料，如今都落入虎口。”滕彪当晚在日记中写道。 <br />
<br />
公盟的个案助理林峥恳求正在把毒奶粉受害者的资料装进编织袋的执法人员：“留个联系方式吧！也许以后我还能要回来！”<br />
<br />
近一年来林峥介入了大量毒奶粉诉讼，一直在与受害者家长联系，办理委托，收集材料，解释法律，在石家庄和北京之间往来奔波了十几趟，每天电话不停，同样的话跟各地的家长一一重复数百遍。 <br />
<br />
最终，民政部门留下了有关毒奶粉的诉讼材料。<br />
<br />
下午2点民政执法大队离开的时候，公盟的两间办公室里只余下4张桌子。存放其他志愿者私人物品的保险箱也被收走。 <br />
<br />
&nbsp;&nbsp;<br />
<br />
公司化生存<br />
<br />
许志永第一次走进公众视野，是因为一个叫孙志刚的年轻人。公盟的成立，也与此不无关系。<br />
<br />
2003年3月20日，大学毕业2年刚刚找到新工作的孙志刚在广州市收容人员救护站遭毒打后死亡。<br />
<br />
这一天刚好是许志永30岁生日。<br />
<br />
2003年5月13日，许志永联合他读博时的同窗好友滕彪和俞江，以公民名义向全国人大常委会递交“对《收容遣送办法》进行法律审查的建议书”，要求废除收容，并建立违宪审查制度。<br />
<br />
一个月后，《城市流浪乞讨人员收容遣送办法》被废除。<br />
<br />
&nbsp; &nbsp; 被媒体大量曝光之后，很多访民都慕名来向他们寻求帮助，而他们却感到无力又无奈，因为能力和精力都有限。这个时候他们便想到成立一个机构，“找志同道合的人组成一个团队，可以更专业，帮助更多的人。”<br />
<br />
2003年7月的一天，著名公益律师张星水向许志永提议成立一个公民组织。<br />
<br />
10月28日，他们注册了“北京阳光宪道社会科学研究中心”，后更名为“北京公盟咨询有限责任公司”。张星水、滕彪、俞江和许志永任理事，网站的名字起名“阳光宪政”。<br />
<br />
中国有许多民间组织，包括很多著名的NGO，比如红枫妇女热线、农家女、惠泽人，在民政登记无门的情况下，被迫选择了工商注册，以“公司”的面貌出现。<br />
<br />
公盟也正是在这样的背景下于 2003年工商注册而成。像很多无法在民政合法注册的民间组织一样，他们只能到工商局注册备案。外部是公司，内部是NGO，两套章程。<br />
<br />
2005年7月，许志永和张星水一同为河北民营企业家孙大午领导的大午集团涉嫌非法吸收公众存款一案进行辩护。<br />
<br />
在该案中对于地方试图干预司法的势力，他们通过组织专家研讨会、进行媒体推广等各种活动把案情公布在阳光底下。最终孙大午被判缓刑，免除牢狱之灾。这次实践为他们以后的公益行动提供了模版。<br />
<br />
几年来，在重大公共事件，都可以听到公盟的声音，看到他们的行动。其中包括，起草宪法人权修订条款建议，关注打工子弟学校被取缔问题，代理黑砖窑受害者国家赔偿，援助毒奶粉的受害者，发起北京律师协会直选活动，并在最近，为邓玉娇、黑监狱受害者、上访者提供援助，等等。<br />
<br />
2003年11月，许志永以独立候选人身份竞选海淀区第十三届人大代表。同为正式候选人之一的北邮信息学院院长郭军告诉他的研究生，不要选他自己，要选许志永，因为许比他更能够做一些社会正义的事情。最后许志永高票当选，成为北京市第一位以独立候选人身份当选的区人大代表，并于4年后成功连任。<br />
<br />
成为人大代表后，许志永的“权力”也更大了。<br />
<br />
在2004年1月的海淀区人代会上，海淀区的财政预算中有一笔12.6亿元的“其他”项目，而当年海淀区的总财政收入才50多亿元，不少代表为此质疑。是许志永在大会上正式要求对“其他”给出解释，并把这个情况提交媒体监督。<br />
<br />
最终，迫于舆论，海淀区财政局对此做出了详细的解释。<br />
<br />
和许志永个人的境遇相比，公盟却一直面临着种种制度困境与无形的压力。<br />
<br />
这个团体一直都没有放弃尝试民政注册，但前后数次申请，都没有回音。由于是工商注册，他们一直都要把捐赠所得作为法定企业收入，缴纳25%的企业所得税（在2008年新税法颁布以前是33%）。<br />
<br />
他们甚至遭遇了一些蹊跷的生活问题。<br />
<br />
最近的一年中，理事滕彪接连遭遇护照被没收、律师执照无法通过年检和被所执教的中国政法大学停课。<br />
<br />
7月21日下午，许志永同时接到公盟公司的房东和他租住房东的电话，被要求马上搬走。<br />
<br />
 <br />
<br />
身陷财税囹圄<br />
<br />
&nbsp; &nbsp; 北京税务两局的行政处罚决定书显示,公盟偷税款项来自于耶鲁大学法学院自2006年以来的四笔资助以及公盟研究员王功权于2009年4月的资助,共计110余万人民币。 <br />
<br />
&nbsp; &nbsp; 北京地税局认为,这些“捐赠”没有按规定申报纳税,偷税达6万余元。北京国税局则认定,这些“捐赠”属于法定企业收入,公盟没有交纳25％的企业所得税,共计18万元。<br />
<br />
按照相关法律,偷税行为可施以50％至5倍的罚款。在对公盟的处罚中,两局同时执行了5倍的最高上限,罚款总额达到142万元。 <br />
<br />
许志永则认为，“我们这样做没有任何违规的地方”。对于采用“预收账款”――这是许多单位常用的逃税手段――他的理由是：税收的征管法律和会计的不一致，我们按照会计的记账准则，这笔钱的性质是专项资助，不是营业收入。<br />
<br />
“有一个很有意思的现象，对于耶鲁法学院同一笔捐款，国税局认为是捐赠，地税局认为是营业收入。如果是捐赠，就只需交所得税，不交营业税和地税。但是，我们还是都按照营业收入交了，该交不该交的我们都交了。” <br />
<br />
许志永坚持公盟绝无偷税的主观故意，“我从来主张应该依法纳税，哪怕法律不合理。”<br />
<br />
一位不愿透露姓名的注册税务师对罚款额度表示不解：“要这样执行，没有几个企业能存活下来！’”<br />
<br />
这位注册税务师还注意到，从2009年1月1日至7月14日，北京国税局一共只对5家单位进行了罚款的行政处罚。“大多数企业都存在（有意无意的）偷漏税问题，为什么特别关爱公盟――这个名为企业而实为公益的小单位，而且处以重罚呢？”<br />
<br />
清华大学公共管理学院NGO研究所副所长贾西津也持同样的质疑，她撰文指出：如果是例行工作流程，税务局随机对纳税单位进行抽查，国税、地税为什么同时并一致地抽查了一个组织的问题？如果是有人向多部门举报，三个部门几乎在同一时间完成各自程序不同、内容不同的查处过程，也是一件小概率事件。 <br />
<br />
　　北京地税和国税分别在24日和30日分别举行了不公开的听证会，但没有给与答复。面对高额处罚,公盟表示无力支付，目前该机构账面余额仅8万元。滕彪表示自己对结果并不乐观，可能会考虑破产，否则公司法人代表许志永可能将面临最高7年的牢狱之灾。<br />
<br />
另一方面，在税务处罚下达三天后，北京市民政局宣布了对公盟下属机构“公盟法律研究中心”的《取缔通知书》：“经查，‘公盟法律研究中心”未经登记，擅自以民办非企业单位名义开展活动……本机关决定对“公盟法律研究中心”予以取缔。”<br />
<br />
同时，公盟遭到了查抄。<br />
<br />
公盟在7月23日提出行政复议申请，要求撤销《取缔通知书》、确认民政局没收公盟财物的行政处罚行为违法，以及归还所有被没收财物和文件。<br />
<br />
而作为执行者一方，北京民政局发言人周立标表示，因为尚未结案，不便接受采访，“至于为什么取缔公盟，你去问执法大队吧。”截至发稿前，民政执法大队尚未回应本刊的采访申请。<br />
<br />
 <br />
<br />
群体困境<br />
<br />
与公盟一同在公益诉讼的道路上比肩而行的，还有许多大大小小的组织和个体。<br />
<br />
公益诉讼旨在对不平等的资源予以调整，关注着社会中处境最差、最边缘化的群体，而限于当前环境，公益律师实际是艰难地在夹缝中生存。他们同样面临恐吓殴打和不为公众知晓的无形施压。<br />
<br />
比如，致力艾滋病等疾病救助的益仁平中心曾遭执法机关以“涉嫌出版”为由入室搜查，其印刷品亦被扣押；曾获得德国之声国际博客大赛最佳中文博客奖的刘晓原，搜狐博客被关闭，后律师事务所被因故停业整顿；而严义明律师甚至在自己的律师事务所里莫明遭到歹徒袭击，以至右肩胛骨骨折，身上另有多处受伤。<br />
<br />
北大法学院妇女法律研究与服务中心的创办人郭建梅律师，一直因其“官方支持”备受同行们羡慕。她在1995年就顺利地成立了这个挂靠在北大法学院的NGO，一步到位地解决了中国NGO最头疼的注册、资金和财务三大问题。<br />
<br />
她认为这都要归功于自己一直深谙“打开通道”这一“狭缝生存原则”：积极主动地与政府部门和国际社会创建合作关系，达成绩效，包括六次受到希拉里接见，以及与联合国建立合作项目。而不久前获得全国妇联资助的1.8万美金所带来的“政治意义”，才是最让她欢欣鼓舞的，“这相当于受到了政府的肯定！”<br />
<br />
可是这位著名作家刘震云的妻子，却在九年前患上了抑郁症。<br />
<br />
“被恐吓、被打，是家常便饭，可最受不了的是三天两头被找谈话，手机还被监听。”郭建梅一个月前又换了一个号码。<br />
<br />
最大的困扰，当然还是工作受到各方面的阻碍。<br />
<br />
“办案非常难，”她说，“往往对手不是有权有势的人，就是黑社会，要么就是村霸、地痞流氓，总之都是有社会关系的人。有时对手甚至是强大的经济利益集团，甚至是国家政府机关。”<br />
<br />
“现有体制下司法难以独立，司法权在一定程度上依附于行政权，而行政权力的强制干预是我们律师所无法左右的。”<br />
<br />
钱，也是绕不开的问题。<br />
<br />
“公益律师也得发工资，也要吃饭呀。”郭建梅说。<br />
<br />
公益诉讼的当事人多是挣扎在社会底层的弱势群体，用刘晓原的话说，“你要钱，他也没有啊。”因此大部分的公益诉讼都是免费代理，而公益律师的经济状况也绝对处于律师群体的最下游。<br />
<br />
危机最严重的是周立太的律所，至今还有上百位农民工拖欠500万元律师费讨要未果，而他们累积的外债已有150万元之巨。至今只能勉力维持。所里年轻律师收入微薄，“一个月就两千多块钱”。而先后离开的年轻律师，“刚好走了108人”。<br />
<br />
郭建梅的情况稍好，她自己是中心主任，每月固定工资六千元，其他律师平均四千五百元。<br />
<br />
而公盟，除了三个全职的行政人员外，所有人都是不拿工资的。许志永唯一的收入来源就是在北京邮电大学任教的每月四千元工资，他把自己买的房出租给别人，租金还不抵房贷，再加上另外租房的租金，每月只剩1800元。<br />
<br />
可在资金紧张的情况下，他们仍然谨小慎微地谢绝了大部分国外捐助，仅接受美国福特基金会、耶鲁大学等“安全”的捐助者。<br />
<br />
前中国政法大学副教授萧瀚指出，这样的经济条件很难吸引更多优秀人才加入。“我们不能给人一个印象说做正义的事情是像苦行僧，是苦哈哈的，一定要让正义的事业是有吸引力的。过上一个幸福的日子，这是每个人一种非常自然的追求。这没有什么不对。”他认为公益律师都是公民英雄，“让公民英雄都做成穷人这种想法不对。”<br />
<br />
“让公益律师体面起来，无论是经济上还是政治上。”这是郭建梅的理想。<br />
<br />
