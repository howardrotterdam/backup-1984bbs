													<h2>罗刹国再次洗劫华商20亿美元资产  天朝会发声吗</h2>
						
						
																											<div id="postmessage_138124" class="t_msgfont">中国商人20亿美元资产在俄罗斯遭洗劫<br />
<br />
2009年06月26日10:45<br />
<br />
<img src="http://img.hexun.com/2009-06-26/119048212.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　5月27日，在俄罗斯西伯利亚城市克拉斯诺亚尔斯克以南425公里的一个村庄，一名中国菜农正从蔬菜大棚里出来。他们将要面对的，很可能是当地部门的突击行动。路透社<br />
<br />
　　“有人跳楼，有人自杀，这么多人倾家当产，可至今什么问题也没有解决”<br />
<br />
　　国际先驱导报记者金微发自北京“价值几百万的货物全没了。”小林（化名）是一名在俄罗斯打拼的中国商人，几年前跟随同乡来到俄罗斯淘金。但没想到的是，莫斯科成了他的恶梦。<br />
<br />
&nbsp; &nbsp; 6月18日，俄罗斯政府宣布将集中销毁价值高达20亿美元的中国“走私”商品，并要求莫斯科市尽快关闭切尔基佐沃市场。这里是数万华商在俄罗斯安身立命的依靠。小林的“几百万货物”就在这批即将销毁的商品中，为挽救自己的这批货物，他上下奔走，但至今毫无结果。<br />
<br />
　　在接受媒体采访时，他甚至不敢透露自己的真实身份。“如果俄罗斯方面知道我接受了媒体采访，我的货物更拿不回来了。”<br />
<br />
　　而小林的背后，是数万名一夜间倾家荡产的华商。<br />
<br />
　　有人跳楼，有人自杀<br />
<br />
　　“何止是20亿美元！6000个集装箱啊！每个集装箱货物的价值都在50万到100万美元之间，这次华商的损失是空前的。”俄罗斯中国浙江同乡会会长倪吉祥在接受《国际先驱导报》采访时颇感无奈，“有人跳楼，有人自杀，这么多人倾家当产，可至今什么问题也没有解决。”<br />
<br />
　　6月18日，俄罗斯总检察院表示，没收的这些华商商品属走私品，其中一些伪劣商品将集中销毁。这一消息令华商感到绝望。有多名华商很气愤地透露，俄方称这些没收的商品是走私的、劣质的，但没收后却以低价在市场上倒卖。<br />
<br />
　　而在倪吉祥眼中，这么多年来，中国商人在俄罗斯一直都如此经营生意，以前尽管遇到过没收商品的情况，但毕竟只是小规模的。上世纪90年代初，倪吉祥就来到了俄罗斯，见证了切尔基佐沃市场由小变大的过程，“从1999年开始，越来越多的中国人涌入这个市场，使得切尔基佐沃急速膨胀，市场里98%的商品都是中国货，很多越南人和俄罗斯人都来到这里为华商打工。”<br />
<br />
　　3万华商沦为替罪羊<br />
<br />
　　据说切尔基佐沃具有20多年的历史，由俄罗斯寡头伊斯梅洛夫创办，目前发展为欧洲最大的零售批发市场。伊斯梅洛夫通过“灰色清关”的方式，吸引大量中国商品进入俄罗斯，由此积累了大量财富。<br />
<br />
　　在市场里经营的华商，除了交纳场地费，还要交各种手续费给伊斯梅洛夫，而伊斯梅洛夫自称会代华商向政府部门交税。“但谁也不清楚伊斯梅洛夫上交了多少税给俄罗斯政府，他可能早已作好了潜逃的准备。”在俄经营酒店的华商晋国田在电话里面告诉《国际先驱导报》。<br />
<br />
　　去年，伊斯梅洛夫被列为俄罗斯政府打击对象，俄检察部门指其组织销售中国走私商品，并扣留了市场上的那批华商的集装箱。倪吉祥告诉《国际先驱导报》，今年俄罗斯也陆续查封了切尔基佐沃市场一些华人的仓库，并没收不少商品。<br />
<br />
　　在俄政府重拳下，伊斯梅洛夫早已逃之夭夭，而华商则成了替罪羊。据估计，切尔基佐沃有7万至8万名华商，而这次遭波及的华商在3万名左右。<br />
<br />
　　问题还在于“灰色清关”<br />
<br />
　　俄罗斯方面称查收的中国商品是走私货，是通过“灰色清关”的方式过来的。但据熟悉中俄贸易的人士透露，俄称华商货物为“走私货”有失公允，责任其实不在中国商人。<br />
<br />
　　所谓的“灰色清关”并非走私方式。据这位人士介绍，上世纪90年代，俄罗斯国内经济萧条，日用品严重匮乏，而中国轻工业产品在俄罗斯极具竞争优势，大大满足了俄罗斯普通老百姓对中低档商品的需求。于是，俄罗斯一些公司专门做起了“包机包税”“包车包税”的生意，也就是说，发货人在我国内发货、交钱，收货人在俄收货，其余中间环节，包括运输、通关、商检等统统由俄罗斯公司解决。俄海关委员会也批准一些“清关公司”专为这种贸易履行通关手续，收取税款。<br />
<br />
　　但问题在于，有些“清关公司”为了偷税漏税，在货物通关后不给货主出具正式的海关报关单和税单，再加上俄政出多门，腐败现象丛生，这为俄警方随意查扣华商货物制造了借口。<br />
<br />
　　对华商的遭遇，在海外打拼十余年的倪吉祥感觉无能为力。“俄一些部门的腐败现象比较严重，他们自己搞‘清关公司’，但当货物到达俄罗斯后，就什么都不管了，不会办理任何手续。”<br />
<br />
　　在俄华商何去何从<br />
<br />
　　与打拼在俄罗斯的华商相比，一群在俄罗斯辛勤耕种的中国菜农，同样逃不出遭俄罗斯地方政府洗劫的命运。<br />
<br />
　　6月20日，俄罗斯《共青团真理报》以“洗劫中国人”为题报道说，俄罗斯车里雅宾斯克州叶特库利区执法部门，动用推土机拆除了中国人在当地建造的“非法蔬菜大棚”。很多中国农民苦心经营的农场，在俄罗斯地方执法部门的推土机下因此被夷为平地，理由则是“证件不全”“不符合规定”等等。<br />
<br />
　　但令这些遭受惨痛损失的中国农民不解的是，当初俄方并没说“非法”，等到他们投入大把打把的资金，苦心经营并有所起色后，俄当地政府却要以非法为由野蛮整治。 <br />
<br />
　　晋国田说，尽管华商给俄罗斯带来了大量的便宜商品，但俄媒体下的华商形象多为负面。这是在俄经营的华商必须认真思考和面对的问题。而对于深受打击的在俄华商和菜农，何去何从成了当下急需作出的选择。<br />
