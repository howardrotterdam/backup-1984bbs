													<h2>中国今年入夏天气反常 四大火炉熄火三冰城发烧</h2>
						
						
																											<div id="postmessage_674080" class="t_msgfont">我国今年入夏天气反常 四大火炉熄火三冰城发烧<br />
<br />
2010年06月11日08:39<br />
<br />
　　昨日,在微博上流传着这样一个段子,“今日气温――四大火炉:武汉21℃、南京22℃、重庆25℃、南昌23℃。三大冰城:哈尔滨35℃。长春34℃,沈阳33℃”,这生动地反映了最近反常的天气。根据江苏省气候中心的统计,南京6月9日最高气温19.8℃,根据历史资料分析,为1905年以来6月上旬日最高气温第四低值（仅次于1911年6月8日、1987年6月7日和1954年6月1日）,创近百年来同日最高气温最低值纪录。<br />
<br />
　　“这个天气真是太反常了!”最近除了高考,大家议论最多的大概就要数天气了,以往南京的这个时候早就酷热难耐了。但今年由于大气环流的异常,副热带高压不强,冷空气总是可以轻而易举地长驱直入,与冷空气相遇后,雨带就“赖”在我国华南和江南一带不走了。而相反,进入5月以来,我国东北地区的气温急转直上。特别是近10天,黑龙江以及内蒙古东部的平均气温比往年偏高4～6℃,前日哈尔滨的最高气温一举达到36℃,迎50年来最热初夏。<br />
<br />
　　从全国来看,截至6月8日,全国春、夏区域各占一半,部分大城市入夏形势各不相同。其中,京津冀地区已于5月中旬提前入夏。南方大部区域已经进入夏天,但上海、南京受近期频繁降雨影响,入夏脚步徘徊不前,仍未入夏。青藏高原部分地区还处于冬季。所以我国目前是冬春夏三个季节同时存在。<br />
<br />
　　从2009年底开始的冬季,寒潮不断,有气象专家称,这个冬季很像是全球变暖进入了一个“冷周期”。而此后,“冷周期”似乎像惯性一样延续下来,南京也经历了从1992年以来最冷的一个春天,4月份还飘起了雪粒子。除了“五一”小长假有些反常的热之外,直到6月,南京仍没能满足5天日平均气温都超过22℃,所以没能达到入夏的硬杠杠。6月还不入夏,在本世纪以来南京还未出现过,但是百年来南京历史上也有6月中下旬才入夏的记录。<br />
