																											<div id="postmessage_158980" class="t_msgfont">谁是幕后的黑手 <br />
&nbsp;&nbsp;<br />
魏京生 <br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 乌鲁木齐事件发生后，中共在第一时间就说是有幕后黑手，黑手论被炒得很热。<br />
究竟有没有黑手呢？许多人不相信。因为中共给别人栽赃陷害时，总喜欢找黑手。前台找不着原因就制造个黑手论，十分方便。狼来了说多了，大家也就不信了。但是还是有很多人会被中共的谎言所迷惑。因为中共的造谣水平也是与时俱进步步高，谣言的后边一定会跟着一些似是而非的说法，来证明谣言是真的。<br />
<br />
这次放出来的风是：中共不会自己搞乱自己。果然如此吗？让我们来简单地回顾一下事件的进程，再来分析这个谣言背后的评论。<br />
<br />
乌鲁木齐事件是大规模的民族冲突。它的前因就是广东韶关的大规模民族冲突。越来越多的境外媒体的报导证实，韶关的冲突不是人民之间的直接冲突，而是受到引导甚至是有意制造的冲突。<br />
<br />
网上的说法多了去了。近年来一些事件的起因就是网络传递的信息，例如瓮安事件、杨佳事件，等等。最近的邓玉姣案件，中共不得不向舆论低头。大家想想中共窝火不窝火？中共有庞大的五毛队伍，他们就不会想到利用网络散布谣言达到政治目的吗？于是网络上就出现了许多诋毁少数民族的谣言，但都没形成气候。于是就尝试着把谣言和行动组的作用结合起来使用。6月26号在广东韶关一举成功。<br />
<br />
综合各方面的报导，韶关的过程是这样的：先制造谣言煽动社会；然后在维族青年追求汉族女青年时，造谣说是强奸；由事先安排好的保安人员出面抓人；再由公安控制的黑社会把毫无防备的维族青年乱棍打死；其他的维族人必然会蜂涌而至保护自己人。这时候，事先受到煽动的大量汉族人也恰好赶到。这样就成功地制造了一起民族冲突。<br />
<br />
韶关的冲突只是前戏，不是正戏。因为韶关的维族人很少，那儿的汉族人不会感觉自己应该受到保护。这就不足以煽起社会普遍情绪。所以在计划中只是前戏。正戏是海外的维族人组织必然要采取行动，保护受迫害的同胞。这就有机可乘。<br />
<br />
可是“世界维吾尔人大会”和它的前身“世界维吾尔青年大会”早就排除了恐怖主义，不符合发生流血冲突的条件。果然，乌鲁木齐事件开始时的游行示威，非常和平理性，甚至有纠察人员防止游行队伍和旁观的汉族发生冲突。<br />
<br />
但是没关系。你热比亚领导的不是所有维族人。和本拉登合作的恐怖组织大有人在。所以，不阻止恐怖组织掺合进来，再由警察破坏游行的组织纪律，恐怖份子自然会大打出手。这些仇恨满腔的恐怖份子自然会惨无人道；这种惨无人道的行为必然会引起汉族人民义愤填膺。整个节目就制作完成了。汉维各族老百姓的生命，就成了阴谋的牺牲品。<br />
<br />
在整场戏中，前半段时黑手都是藏在幕后，很难被发现。但在最后关头，政府必须出面配合，否则出不来效果。所以在韶关和乌鲁木齐，在骚乱发生但还没达到充分效果之前，官方和警察都很奇怪的现场缺席。特别是乌鲁木齐，竟有四、五个小时之久。<br />
<br />
按中共自己的说法，境外组织和国内通电话中共一直在监听。大量的砖头是用卡车运进乌鲁木齐市区的。会出什么事情，连外行也一清二楚。为什么事发后四五个小时才有武警干预？为什么事先拒绝外地的武警调入乌鲁木齐？这恰好证明了谁是黑手。<br />
<br />
至于说“中共怕乱，不会自己搞乱自己”的谣言，那真是个缺乏常识的胡说八道。纳粹德国是谁搞乱社会的？乱中取胜、混水摸鱼是独裁政治的必修课。当年的希特勒就是靠着把犹太人当人民公敌，才骗得德国老百姓团结在纳粹党的周围。当年的毛泽东，是靠着把地主资本家制造成人民公敌，才骗得中国老百姓团结在共产党的周围。<br />
<br />
大跃进饿死几千万人之后，地主资本家作为人民公敌失效了，又及时地揪出了党内走资派。没有目标就没有独裁，这是个规律。现在官僚资产阶级自然而然的成为了人们的目标，中共自己成了目标，这就不能接受了。<br />
<br />
于是。少数民族就成了最有价值的目标。以此来替代官僚资产阶级，拯救共产党。能不能像当年的希特勒一样挑起族群仇恨，就成了中共生死存亡的大课题。选择维族而不是藏族和蒙古族，是因为在西藏，汉人是少数，只会逃跑而不会有几个回合的战斗，挑不起大事儿来。内蒙的蒙古族太少，而且大部份是汉人了，也挑不起大事儿来。新疆的两族势均力敌，就成了选择的目标。<br />
<br />
