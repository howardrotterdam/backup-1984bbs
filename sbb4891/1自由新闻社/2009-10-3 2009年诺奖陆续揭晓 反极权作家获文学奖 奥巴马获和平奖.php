																											<div id="postmessage_269857" class="t_msgfont">诺贝尔奖将于下周陆续公布得主，而就在当地时间1日，一本幽默杂志赶着为诺贝尔奖“暖身”。据台湾《联合报》报道，哈佛大学桑德斯剧场举行了“搞笑诺贝尔奖”颁奖典礼。得奖研究千奇百怪，包括发现取名字的乳牛产量较高，在酒吧打架拿空酒瓶K人杀伤力最强，或是发明能改为防毒面具的胸罩等。<br />
<br />
<br />
　　据悉，搞笑诺贝尔奖(Ig Nobel)每年抢在诺贝尔奖公布前一至两周颁发。其主办单位为《不可能的研究记录》(Annals of Improbable Research)，名称则源自诺贝尔相似字noble(高贵的)的反义词“ignoble”(卑贱的，不光彩的)，连取名都不忘幽本尊一默。<br />
<br />
<br />
　　该奖创立于1991年，鼓励人类以超乎寻常的想象力和创造力，用科学方法进行“乍看令人发笑，之后发人深省的研究”。每年有10个领域的研究获奖，包括传统诺贝尔奖的五个领域：物理、化学、医学、文学与和平，再加上公共卫生、工程、生物和其它学科。<br />
<br />
<br />
　　难得的是，虽然乍看之下得奖似乎并不光彩，主办单位也表示如果得奖人觉得受辱，他们会撤销提名。但事实上多数获奖者都很高兴，主办方还会请来诺贝尔奖得主担任评委。比如去年诺贝尔经济奖得主克鲁格曼这次就获邀出席。<br />
<br />
<br />
　　下面是香港《文汇报》总结的部分此次“搞笑诺贝尔奖”得主，其研究内容真可谓“大开眼界”：<br />
<br />
<br />
　　兽医学奖：英国纽卡素大学的道格拉斯和罗林森。得奖原因：发现有名字的乳牛产奶量较高。<br />
<br />
<br />
　　和平奖：瑞士伯尔尼大学的博林格及4名同僚。得奖原因：发现空啤酒瓶比盛满酒的瓶子更具杀伤力。<br />
<br />
<br />
　　医学奖：美国加州的昂格尔医生。得奖原因：60多年来每天不断敲打左手指关节，跟右手对照，看看会否如母亲说的导致关节炎。今年83岁的他说：“我两只手的指关节没任何关节炎迹象，可以向天堂说：妈妈，你错了。”<br />
<br />
<br />
　　经济学奖：包括冰岛央行在内的4间冰岛银行的董事、高层和核数师。得奖原因：证明小银行可迅速变成大银行，反之亦然，并证明同样的事可发生在整个国家的经济上。<br />
<br />
<br />
　　化学奖：墨西哥国立自治大学的莫拉莱斯及2名同僚。得奖原因：用墨国著名的龙舌兰酒制造钻石，方法是在高压器皿内加热龙舌兰酒，制成钻石薄片。<br />
<br />
<br />
　　文学奖：爱尔兰警队。得奖原因：向一名他们以为是道路驾驶重犯的男子Prawo Jazdy发出逾50张告票，其实“Prawo Jazdy”在波兰语是驾驶执照的意思。<br />
<br />
<br />
　　公共卫生奖：美国芝加哥的博德纳里及2名同僚。得奖原因：发明一款胸围，遇上紧急事故可变成两个防烟面罩，救己救人。<br />
<br />
<br />
　　数学奖：津巴布韦储备银行及行长戈诺。得奖原因：印制的钞票面额从1分到100万亿不等，令人民每天要处理大量数字。</div>

							
							
							
							
