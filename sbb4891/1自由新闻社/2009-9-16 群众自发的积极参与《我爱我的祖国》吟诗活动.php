<br />
　　《我爱我的祖国》出身“失意”<br />
<br />
　　这场狂欢源自“天涯社区”征集网友作品来做国庆网络狂欢晚会，诗歌爱好者“晨歌牧马”向组织者发送了自己写的一组诗，结果因为“不够娱乐”而被毙。<br />
<br />
　　愤愤不平之余他干脆发帖称该晚会有“丑恶内幕”。“晨歌牧马”说，“我好歹也是个校园诗人，校报上发表过好几首诗呢！”为了证明自己的诗歌水平，他还贴了此次投稿的作品《我爱我的祖国》，“让大家来评理”。“我爱我的祖国，她像一座大山。站在山顶，放眼远眺。江山如此多娇，风景处处都好……”<br />
<br />
　　这首短短的4段式20行的“爱国诗歌”确实中规中矩“不够娱乐”，跟帖的网友中有人表示同情，也有人表示落选是“实至名归”。<br />
<br />
　　没想到，网友争当“朗读者”<br />
<br />
　　改变发生在一位名为“随缘也好”的网友把其朗诵这首诗的音频放到了网上，“楼主实在太有才了，我不忍心这么好的作品死于非命，特意朗诵了一下”，这位网友的朗诵的确字正腔圆，被认为是很标准的“CCTV式朗诵”，本来这首诗并未招来多大关注，可没想到配了“CCTV音”后却“身价倍增”。<br />
<br />
　　自从有此朗诵之后，前来围观的网友逐渐增多。“太有才了，听第一句我的茶就喷出来了”、“在这首大气磅礴震古烁今的史诗面前，李白杜甫显得那么渺小”……甚至有人发出“此帖不红，天理不容”的预言。接下来就有“好事者”倡议“要是不同的人用不同的声音来朗诵这首诗，那会是多么壮观啊！”这立即引来了众多网友的响应，倡议才发出一小时，就有人拿出他刚录的伦敦音版，所谓伦敦音并不是用有伦敦口音的英语来朗诵，而是装成英国人进行中文朗诵。<br />
<br />
　　此后模仿曾轶可的绵羊音版本、蜡笔小新版、重庆方言版、广东方言版陆续涌现，还有模仿孙悟空、威震天、擎天柱、樱桃小丸子等经典声音来“祝寿”的，连“易中天”都被模仿了……一个原本很普通的帖子变成大热帖，截至昨日下午三时，该贴浏览量已经超过70 多万，回帖近5000条，甚至被称为“国庆前最牛网帖”。一位随后加入到朗诵队伍中的网友说，朗诵的真是一个比一个牛，“看了这个帖子让我深深感觉到我爱我的祖国，我爱我的同胞。”<br />
<br />
　　已有百余种朗读方式<br />
<br />
　　原本有网友建议“希望在国庆前能凑齐56个版本(56个民族)”、“向60周年献礼，有60种版本就更完美了”，可没想到网友们的创意“如滔滔江水”连绵不绝，有网友数了一下，说是至少已经有过百个版本了，一个新加入该帖的网友表示“都是大才啊，这么多个版本听下来嘴都笑抽筋了”。网友还总结出了“十佳配音演员”，“CCTV版”的创作者“随缘也好”被封为“一代神帖的开山鼻祖”――“主旋律般浑厚的声音，极佳的朗颂技巧，合理的配乐，有化腐朽为神奇的效果”；而“雨里的风”以一口地道的伦敦腔“倍儿有面子”，被评为“承上启下，由此，人才济济，英雄辈出”；而曾轶可的绵羊音版，是一个男孩子颤抖着声音模拟出来的，被赞“最有才”；还有“拼组人生”的“经典猴哥配音”被赞“四大经典之滥觞”……记者截稿时，神通广大的网友仍在陆续奉献各自的“个性版”――“小女子含羞献声，妩媚版我爱我的祖国，献给祖国，献给天涯”，“念得不好，请笑纳”……虽是大家以娱乐为主，但一位网友的回帖也引起了大家的共鸣――“我在八卦坛里被感动了，来自草根的声音就是如此的真实和赤诚，任何表演家在她面前都会黯然失色，我是一介草民，我爱我的祖国，光明与黑暗，善良与邪恶，她总无怨言，一路承载着，这壮丽的山河，我爱我的祖国，与任何人无关……”(张艳) <br />
