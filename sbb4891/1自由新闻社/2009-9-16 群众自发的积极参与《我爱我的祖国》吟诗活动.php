																											<div id="postmessage_243033" class="t_msgfont">“吟一首诗不难，难的是用各种语言狂吟……”关键是这首诗并非出自名门，而是一位诗歌爱好者落选的作品。天涯网友“晨歌牧马”写了一首《我爱我的祖国》，没想到用不同语言朗诵他的这首诗歌成了今年最大的民间诗歌狂欢。截至昨日，除各地方言版陆续出现外，还有变形金刚版、蜡笔小新版等更具个性的朗诵版本，就像“寂寞”一样，最后大家追捧的不是这首诗，而是以这首诗为载体变着花样来自娱自乐的一种行为。<br />
