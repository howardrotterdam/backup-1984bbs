2009年2月2日至6日，重庆市第五中级人民法院公开开庭审理了重庆市人民检察院第五分院指控被告人文强犯受贿罪、包庇、纵容黑社会性质组织罪、巨额财产来源不明罪、强奸罪一案，并于同年4月14日作出一审刑事判决，认定文强犯受贿罪，判处死刑，剥夺政治权利终身，并处没收个人全部财产；犯包庇、纵容黑社会性质组织罪，判处有期徒刑十年；犯巨额财产来源不明罪，判处有期徒刑八年；犯强奸罪，判处有期徒刑四年，决定执行死刑，剥夺政治权利终身，并处没收个人全部财产。一审宣判后，文强提出上诉。重庆市高级人民法院经依法公开开庭审理，于今年5月21日作出刑事裁定，驳回文强上诉，维持原判，并依法报请最高人民法院核准。<br />
