																											<div id="postmessage_376265" class="t_msgfont">天下杂志民调显示：6成1民众担心经济倾中 7成5年轻人自认台湾人 <br />
<br />
 国民党在县市长选举受挫、得票率大减，近日多项民调显示败选和马政府政经政策全面倾中有关，《天下杂志》昨天公布的最新民调指出，高达六成一的民众担心台湾经济过度依赖中国大陆。<br />
<br />
《天下》民调另一数据也值得马政府省思，六十二％受访者认为自己是「台湾人」，自认「既是台湾人也是中国人」的有廿二％，自认是「中国人」的仅八％；其中，十八到廿九岁的年轻族群认为自己是台湾人更高达七十五％，为同类调查的新高。<br />
<br />
对于《天下》调查结果显示台湾认同愈来愈高，不少人认为值得马政府警惕，若马总统执意维持全面倾中路线，未来选举恐将面临更严峻的挑战。<br />
<br />
《天下杂志》对此则引述亚太和平研究基金会董事长赵春山解读说，以往选择「是中国人也是台湾人」的比率最高，牵涉的是对中华文化的认同，和汉民族血浓于水的感情，如今在国人印象中，谈到「中国」，代表的就是中华人民共和国，因此更强化了台湾人对这片土地的认同感。<br />
<br />
统独两极光谱 有往独移动倾向<br />
关于统独立场，受访民众被问到希望台湾与大陆的关系是独立、统一或维持现状时，主张尽快独立者达十一％，尽快统一为二％，维持现状者则高达七十八％，进一步追问发现，希望永远维持现状、未来逐步走向统一，或未来逐步走向独立，各有卅五％、十％和卅三％。<br />
<br />
《天下杂志》认为，在统独之间的两极光谱，有往独立移动的倾向。国人的政治主张面对两岸经济现实，似乎出现相互矛盾和拉扯。政大中国大陆研究中心主任王振寰比喻说，这就像男女透过交往相互了解，彼此做朋友，但台湾又希望保持独立的个体，不愿步上红毯。王振寰分析，不能否认的事实是，中国不能让台湾独立，台湾不愿被并吞，希望能和平共存，那未来会呈现何种情势？他表示，需要时间和智慧去解决！<br />
<br />
贫富差距扩大 逾九成民众忧虑<br />
《天下杂志》昨天公布的二○一○国情调查，涵盖经济、政治、社会各角度，结果显示国人最在乎的是社会上贫富差距的扩大。高达九十三％民众表示，最忧虑的事是贫富差距扩大，对此表示忧虑的比率也创历史新高，较五年前暴增了近两成。<br />
<br />
经济上，民众对目前经济状况不满意者远大于满意者，超过七成不满意，只有两成满意。不过，三十％的人认为，自己的经济状况明年会变好，为过去十三年来新高。认为明年会更坏的则有大幅下降趋势，从去年的五十七％降为三十％。<br />
<br />
愈是南部民众 忧虑程度愈明显<br />
天下杂志调查也指出，民众忧心台湾向中国经济倾斜达到六十一％，忧虑程度同样创历史新高。且越是南部民众，担心的状况就越明显。<br />
<br />
赵春山认为，以中国和台湾的经济规模相较，国人的疑虑是正常的，这也提醒政府和大陆签署两岸经济合作架构协议（ＥＣＦＡ）时，必须清楚对台的利弊优劣，不能照单全收。尤其未来要做好风险控管，市场要走向多元化，不能把鸡蛋全放在同一个篮子内。<br />
<br />
经济状况不佳，政府也未拿出有效对策，《天下》调查显示，民众认为政府最应立即改善的前三名项目，分别是「提升经济实力」、「减少贫富差距」和「改善治安」。至于台湾的危机，民众列举的前三名一如过去两年，分别为经济衰退和贫富差距拉大、政党恶斗、政治贪污腐败。值得注意的是，虽然两岸关系从紧绷走向和缓，但忧虑台湾国际地位遭边缘化窜升到第四，从十二％上升到十七％。<br />
<br />
被问及谁该为上述危机负责时，廿一％的受访者认为是人民自己，两成直指总统，排名第三的是执政党，显示国人以选票让国民党全面执政，也盼总统和他所领导的执政党能负起完全责任。<br />
<br />
不满意马施政 快速飙到六成六<br />
因此，人民对马英九施政满意者持续减少，从去年卅三％减至廿七％，不满意者则骤增十一个百分点，从五十五％飙到六十六％。<br />
<br />
此外，马英九当选国民党主席，不惜以全面改选中常委宣示查贿防弊的决心，但甫落幕的三合一选举仍是贿选频传，国人的感受也反映在《天下》的调查中，六十一％的受访者认为马坚持的国民党清廉改革无效，仅十八％认为有效。</div>

							
							
							
							
