																											<div id="postmessage_685724" class="t_msgfont">搜狐体育讯 朝鲜队，历来在外界的眼中属于一支不为人知的神秘之师。也正是这份神秘，让媒体当中产生了许多有关他们的离奇传说，然而事实上的情况却和媒体报道有着极大的出入。本文就5个关于朝鲜队的传说进行拨乱反正。<br />
<br />
　　1.输球后得去挖煤？<br />
<br />
　　一名“前朝鲜队主教练”披露朝鲜队的秘密：“如果朝鲜队在世界杯上能获得胜利，那朝鲜球员就会得到荣誉，但如果他们失败了，面对他们的将会是惩罚，也许会被派遣当煤矿工人。”然而在几日后的记者访问中，朝鲜队主帅金正勋对这一说法给与了简短有力的驳斥：“荒谬的胡说!”<br />
<br />
　　比起法国总统萨科齐所说的法国队输球后一天内球员们不准见老婆的说法，这则出自美国媒体关于朝鲜队惩罚制度的新闻，显示了朝鲜队球员们的无比巨大压力，但从视朝鲜为眼中钉的美国人口中，也许对朝鲜球员输球后最严厉的惩罚不是去煤矿挖煤，而是直接拉上刑场枪毙才能一解美国人的心头之恨。<br />
<br />
&nbsp; &nbsp; 2.月薪12块？<br />
<br />
　　世界杯开赛前，曾有媒体为朝鲜队算了一笔帐，结果发现朝鲜国内一名高水平的足球运动员的月薪折合成人民币只有12元。<br />
<br />
　　事实上，朝鲜国内的人均收入水平确实很低，一般普通工人一个月的工资也就100-200朝元之间（约合人民币2.9-5.9元）。但低收入同时也意味着朝鲜国内的低物价，要知道在朝鲜一台电视机的价格只要20元人民币，购买一公斤大米则只需要人民币2分钱。朝鲜现在的国内环境和我国计划经济凭票购物的时代极其相似，就算月薪再高，没有票依然买不到东西。于此同时，朝鲜队员们的生活水平还可以通过国家分配得到保障，反观改革开放了30年的中国，却有相当一部分的女足运动员每个月的薪水只有1000元，无依无靠的她们和朝鲜队员相比，不知道谁更悲惨。<br />
<br />
&nbsp; &nbsp; 3.入住难民营？<br />
<br />
　　世界杯期间，在其他国家队大多入住奢华的大学校园或度假村的时候，朝鲜队却选择了在南非的东兰德腾比萨地区扎营下寨，而那一地区属于难民区。<br />
<br />
　　和豪掷2000万英镑入住5星级酒店的英格兰队，以及入住每晚单间价格达到420欧元的豪华度假村的法国队相比，财力有限的朝鲜队的住宿条件当然和他们无法相提并论。然而实用是朝鲜队首要看重的地方，要知道荷兰队和德国队选择的酒店虽然和英格兰法国同样豪华，但身处闹市和干燥的天气则让他们伤透了脑筋。<br />
<br />
&nbsp; &nbsp; 4.写下血书出征？<br />
<br />
　　出征世界杯前夕，朝鲜队队员们在朝鲜最高人民会议常任委员会副委员长杨亨燮和劳动党中央书记金仲麟的接见后，曾写下“血书”，抱着必死的决心发誓一定要在世界杯赛场上取得胜利。<br />
<br />
　　身体是革命的本钱，但精神却是革命的灵魂。一支球队如若本身没有强劲的实力，要是还没有必胜决心的球队，任何时候都无法想象他们能够获胜。朝鲜队的“血书”，很大程度上说明了他们对胜利永无止境的渴望。反过来想想，如果咱们的国足也能够在大赛之前立下军令状，也许“恐韩症”、“黑色三分钟”以及“打平出线却打死不出线”的悲剧就不会发生。<br />
<br />
&nbsp; &nbsp; 5.比赛阵容领袖决定？<br />
<br />
　　有报道称，朝鲜队世界杯每场比赛的首发阵容，都必须经过主席金正日过问后才能最终定夺。<br />
<br />
　　金正日本人对朝鲜足球确实相当关心，每逢国家队到国外参加大赛，金正日都会责成相关领导在待遇和配给上给与照顾。而女足世界杯期间，金正日也给朝鲜女足队员们践行。但是实际上，世界杯上朝鲜队的出场阵容均是教练金正勋根据球员的状态而一人安排。况且作为一国领导人，金正日有什么时间关注朝鲜队的每场比赛且对首发名单发号施令？</div>

							
							
							
							
