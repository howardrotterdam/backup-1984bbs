王文：毛新宇是谁的孩子？<br />
<br />
近来，网民知情者贴出惊天秘闻，说：“毛新宇不是毛泽东的孙子，而是他的儿子”，引起了网民的激烈反应。大部分网友是相信这一消息的。我本想看一看邵华、毛新宇的表态，因为对她们来说，他们太想否定这个消息了。而且，假如这个消息是“造谣”，那么，他们想揭穿这个“谣言”太简单了，只要做个基因鉴定，一公布，谁还敢议论这事？但是，他们没这样做，不但没做，而且连个屁也不敢放，这说明什么？不敢吗！<br />
<br />
如果这仅仅是毛泽东、毛岸青、邵华之间的下半身的事，我也不想说什么了。但不是。现在，毛新宇一直以毛泽东嫡孙的名义到处招摇撞骗，妄图利用毛泽东的僵尸捞取政治、经济、名誉、色情资本，这可是坑害全国人民的事情，这就是个国家大事了。作为一个有良知的中国人，不能不把这件事情搞清楚。下面，我以中国公开发表的资料为证据，对这件事进行分析。当然，中间难免有一些推论，就算抛砖引玉吧。<br />
<br />
一． 毛泽东多次讲过，他“绝后”了。<br />
<br />
1959年7月23日，毛泽东在庐山会议上批彭德怀时，讲了关于他绝后了的问题。他郑重其事的对全体中央委员们说：“始作俑者，其无后乎。我无后乎? 中国的习惯，男孩叫有后，女孩不算。我一个儿子打死了，一个儿子疯了，我看是没有后的。…… 始作俑者是我，应该绝子绝孙。”以后他又多次讲过。例如，他对康生说：始作俑者，其无后乎，我就绝了后。我们知道，毛泽东封建意识很强，他不认为女儿是他的“后”，他认为儿子、孙子才是他的后。但毛岸英虽然死了，可还有儿子毛岸青，毛泽东怎么会说自己“绝后”了呢？显然，他知道毛岸青不能生育，毛岸青生不了儿子，毛泽东就不可能有孙子，这不是“绝后”了吗。毛泽东多次将这件事挂在嘴上，君无戏言，这可不是闹着玩的。毛泽东作为父亲，当然对自己的儿子毛岸青的生育能力了如指掌。从毛泽东亲口说的话里，我们可以清楚的知道毛岸青没有生育能力。<br />
<br />
邵华毛新宇等人一直说，毛泽东是伟大的天才，一贯正确。那么，他关于自己“绝后”了的说法也一定是正确的，这个预言也一定是天才的准确的。既然这样，怎么会冒出个孙子来呢？这个孙子是假的呢？还是毛泽东预言不正确？二者必居其一。请问邵华，既然毛泽东的孙子是真的，那他关于“绝后”的说法就是错误的了，那你怎么说他一贯正确呢？既然他一贯正确，那你的那儿子就不是毛泽东的“后”，他是谁的后呢？<br />
 <br />
二．毛岸青确实“有病”<br />
<br />
毛岸青的精神、肉体都“病”的很重，这是公开的秘密。有文章说毛岸青是精神分裂症，有人说他是“傻子”，有人说他是“神经病”。他的身体也很虚弱，经常住院治疗。连和邵华结婚、都是在大连市医院里住院时搞的。这足以说明，毛岸青的身体状况很糟糕。有人可能说，有病不一定就没有生育啊！可我们看一看毛岸青的婚恋史，他是个正常男人吗？<br />
<br />
毛岸青1923年11月2日出生，1960年和邵华结婚时，他已经37岁了。此前，他没有婚恋过。试想，一个“ 皇子”，能拖到37岁结婚吗？如果他是“地富反坏右”的儿子，没人嫁他，拖到37岁结婚，咱信。可他是“当今皇上”的“独子”啊！怎么解释？唯一的解释就是他“根本没有那个能力”。而且，毛岸青与邵华结婚，也是毛泽东亲自安排的儿媳妇刘思齐撮合的。毛岸青根本不愿意谈恋爱。一个男人活到37岁了，见到像邵华这样的21岁漂亮姑娘送上门来都不动心，还要嫂子催，父亲劝。为了催毛岸青娶邵华，毛泽东还亲自给毛岸青写了一封长信呢！如果毛岸青是个正常男人，毛泽东用得着这样吗？<br />
<br />
毛岸青与邵华1960年结婚。到1970年1月17日才生了毛新宇，十年啊！又是怎么回事？当时，谁不希望毛泽东添个孙子啊？邵华不想吗？怎么就是怀不上？这又说明，毛岸青没有生育能力。<br />
<br />
如果在怀毛新宇前，毛岸青恢复了性功能，能生子了，那么邵华生了毛新宇后，为什么就停下不生了？那时可没搞什么“一胎化”啊，以后搞，也搞不到帝王家呀！请邵华解释一下，如果毛岸青有生育能力，为什么不接着给“伟大领袖”再添个大孙子啊？邵华只生毛新宇一个，怎么那么巧，毛岸青早也没生育能力，晚也没生育能力，单单怀毛新宇那一刻有了生育能力。事实说明，毛岸青没有生育能力。<br />
<br />
三、 从邵华的母亲、姐姐之品行度邵华之心思<br />
<br />
邵华的母亲张文秋，先嫁给山东刘谦初，生女儿刘思齐又名刘松林。又嫁给陈振亚，生女儿邵华，前后不过几年。邵华本应随父姓陈，她却叫张少华，又改为邵华。中国人很重视姓，哪能把姓改来改去？为什么不随父姓？是不是父亲不愿认这个女儿？为什么父亲不愿认？邵华的生父到底是谁？这只有她母亲张文秋知道了。<br />
<br />
邵华的姐姐刘思齐先嫁给毛岸英，毛岸英尸骨未寒，他就另嫁别人了。<br />
<br />
邵华的母亲、姐姐都不是中国传统女人的类型。邵华的母亲张文秋是和我奶奶同龄的人。我爷爷被杀时，我奶奶才30多岁，但她没有改嫁，泪一把，汗一把，将我父亲养大成人。受了多少欺凌啊，但她至死不改嫁，一直守寡到80多岁逝世。这才是那个时代的女人。近朱者赤，邵华出生在那样的家庭里，家庭所有成员都没有传统女人的贞操，到邵华反倒有了？能守住一个神经病丈夫一辈子？且这个傻子还丧失了生育能力，傻瓜才相信这样的鬼话。<br />
<br />
四．从毛泽东的性史猜他与邵华的关系<br />
<br />
毛泽东“风流成性”。如果他是老百姓，则会被称为“流氓成性”，而且，他可能早就被以“流氓罪”判处死刑立即执行了。<br />
<br />
他十几岁就与本地姑娘结了婚。可他停妻再搞上了杨开慧。杨开慧还为他生了三个儿子，但她还没死呢，又搞上了贺子珍。贺子珍还没离呢，又搞上了江青，江青还年轻呢，他又搞了不知多少女人。直到临死80多岁的老翁，还霸着年轻貌美的张玉凤欲。<br />
<br />
这么个男人，守着如花似玉的儿媳妇，想着这个儿媳妇一直没有怀孕，看着自己的傻儿子呆呆坐在那里傻笑，他会干什么？君子度之可也。<br />
<br />
五．从江青对邵华的态度看毛泽东与邵华的关系<br />
<br />
江青对邵华很不好，这是大家都清楚的。仅举一例，1966年7月26日，中央文革在北京大学召开万人大会，江青在会上发言，刚说了几句，就转向猛批邵华和她的母亲，甚至说：“张文秋的女儿张少华，自己说她是毛主席的儿媳妇，我们根本不承认”（见陈伯达回忆录）。当时江青权利很大，一句话就会让人死啊，她为什么对张文秋和张少华这么恨？江青自己没有儿子，用夺嫡之争没法解释，唯一的解释就是女人之间的争风吃醋。江青作为毛泽东的妻子，当然对毛泽东与邵华的关系很关注。如果是正常的公公与儿媳的关系，婆婆会吃醋吗？会吃醋到想把她和她母亲整死的地步吗？<br />
<br />
话说回来，如果江青手里没有毛泽东与邵华私通的铁证，她敢公开这样做吗？毛泽东非打死她不可！但她这样做了，毛泽东一句话也不敢说，这是为什么？明摆着嘛。<br />
<br />
六．从邵华对毛泽东的吹捧看他们的关系<br />
<br />
邵华对毛泽东的吹捧，已到了肉麻的地步。请各位看一看她发表的文章，这哪里是儿媳妇夸公公啊！不是得到过切入骨髓之爱的人，怎么也不会说这种肉麻的话。看一看毛泽东的女儿、女婿们，他们都说不出这种肉麻的话，唯独她邵华！这是为什么？难道邵华不知道毛泽东是个什么人？不知道他杀死多少人、害死多少人？不知道他当面是人，背后是鬼？像邵华，都能爬到“将军”的人，难道糊涂到连这么点识别能力都没有？不可能吗！只有一种解释，“爱液”的作用呗！<br />
<br />
七．从毛新宇不是傻子看他与毛岸青的关系<br />
<br />
从遗传学的角度看，傻子生的孩子，仍然是傻子的机率很高。如果不是傻子，起码也不会是个聪明人。但是我们看毛新宇，又是研究生，又是写书，又是演讲，吹捧毛泽东，吹捧自己，连自己刚出生的儿子也变着花样吹捧到天上，这哪里像毛岸青？而且，刚搞上山东姑娘郝小姐，又搞上江苏姑娘刘小姐，而且怀孕生了子，也是“ 风流成性”，这哪里像到了37岁还不会“男女之事”的毛岸青？再看看他的外表，一点也不像毛岸青吗！请邵华解释一下，你们不是一直说：“龙生龙，凤生凤，老鼠生儿会打洞”吗？而且，还惨杀了怀疑这一说法的遇罗克。那么，请问邵华，毛新宇是“龙生的龙”呢？还是傻子生的傻子呢？<br />
<br />
八．毛新宇到底是谁的儿子<br />
<br />
这个问题，只有邵华心里清楚，也可能连邵华也不清楚。我们想弄清楚，只有用推理了。可能有误，请谅解。<br />
<br />
第一种可能，是毛泽东的儿子。读过“知情者”文章之人，会认可这一说法。没法辩驳吗！读了我的文章的人，也会加深对这一结论的认同。为节省读者的时间，就不重复了。<br />
<br />
第二种可能，不是毛泽东的儿子，而是另有其父。<br />
<br />
据动物学家的科学论断，灵长类动物的孩子死亡率很高。以猴子为例，公猴子经常伤害不是自己交配生下的小猴子。母猴子们为了不让自己的孩子被其他公猴子咬死，便尽可能广的和公猴子们交配。这样，所有与这个母猴子交配过的公猴子，都以为这个母猴子生的小猴子是自己的孩子，因而不会咬死他，还会保护它。这个连母猴子都懂的道理，邵华将军当然会懂。<br />
<br />
读到这里，我们是否可能这样推论：邵华很想生个儿子，“母以子贵”吗！可毛岸青没有生育能力，怎么办？当然最好是从毛泽东那里得到“种”。以邵华的相貌和能力，配上毛泽东的为人，这事能办到。如是，他们相爱了。毛泽东在1966年横渡长江，体力甚健，此后，他打倒刘少奇，春风得意。我看过当时的报纸，医生检查毛泽东的身体，说他能活到１４０岁，全国人民还为此庆祝了好一阵子呢！因此，他为邵华“种”上的可能性是很高的。但别忘了，在他俩中间还有个江青呢！除了江青这个妻外，他当时还有谢静宜等一大群妾呢！毛泽东虽然想尽快给邵华“种”上，但那还有个“天意”的问题。他自己多次说了，他“绝后”了，他可是“一句顶一万句”啊，“老天爷”怎能不安排照办。你看毛岸英，躲在后方的防空洞里，警备森严，彭德怀等一大帮人毫发未伤，他却硬给烧死了。这不是天意？你看毛岸青，硬就傻了，而且，丧失了生育能力，这不是天意？像毛岸龙，硬就从地球上蒸发了，天意难违啊！所以，毛泽东得空就给邵华下“种”，可就是“种”不上。眼看毛泽东一天天老了，邵华也感觉到毛泽东一次比不上一次棒了。怎么办？天意难违，既然老天爷要让毛泽东“绝后”，我一个弱女子能有什么办法？只好另寻“种源”了。但怎样防备毛泽东的怀疑呢？反正你毛泽东已插上一腿无数次了，我与其他男人好上，赶快怀孕生子，你老毛肯定误以为是自己的“种”，到时孙子一抱，不用担心“绝后”了，高兴还来不及，怎么会查查是不是你的“种”？等到孩子长大，你想查查时，你不就早见马克思去了。想到就干，追求邵华的男人如过江之鲫，随便抓几条，哪个不乐哈哈？就这样，1969年，她怀孕了，并于次年生了毛新宇。毛泽东以为是自己的儿子，当然乐不可支，并赐名毛新宇，以示与毛远新同为子侄辈。至于毛岸青，傻瓜一个，怎么会明白孩子是怎么来的？有人喊爸爸，就傻笑一通得了。<br />
<br />
邵华这一招真高，竟瞒过了毛泽东。既然毛新宇不是毛家的后，那毛泽东发的关于他“绝后”了的毒誓，也就不会在毛新宇身上灵验。所以，毛新宇又可以生孩子传“ 后”了。邵华呢？更因为给“毛”家留了“后”，而备受恩宠，一直爬到“将军”。真是皆大欢喜。我以前一直不明白，邵华“将”过什么“军”？她能“将”什么 “军”？怎么就官拜了“将军”？现在我明白了，她连毛泽东这个“君”都能“将”，应该叫“将君”才是，叫“将军”都屈了材。<br />
<br />
九．三点结论<br />
<br />
到这里，可否得出三条结论？<br />
<br />
１． 毛新宇不是毛岸青的儿子。<br />
<br />
２． 毛新宇有可能是毛泽东的儿子。<br />
<br />
３． 毛新宇也可能不是毛泽东的儿子，他可能是邵华的某一位情夫的儿子。<br />
<br />
十．六条呼吁<br />
<br />
１． 请邵华尽快公开向全国人民讲明，毛新宇到底是谁的儿子。<br />
<br />
２． 请毛新宇公布他和毛岸青的基因鉴定报告。<br />
<br />
３． 在毛新宇的基因鉴定报告公布以前，毛新宇停止使用“毛主席孙子”的称号。<br />
<br />
４． 如不回应以上三条，说明毛新宇是杂种一个，他和母亲邵华欺骗了天下百姓，实为窃名盗利偷权之贼，则天下百姓人人可得而诛之。<br />
<br />
５． 了解内情的人们，尽快公开您所知道的这方面的秘密，这是对人民负责的义举。<br />
<br />
６． 请网友们将此文广为传播，最好直接转给毛新宇、邵华等当事人，并敦促他们尽快作出反应。<br />
<br />
附：知情者：《毛新宇不是毛泽东的孙子，而是他的亲生儿子》<br />
<br />
毛泽东的儿子毛岸青不但是个神经病人，也彻底丧失了性功能。邵华与毛岸青结婚后，非常痛苦。但离婚吧，不敢，担心被毛泽东害死。但总不能就这样当一辈子老处女吧！无奈之下，她到公公毛泽东面前，哭诉毛岸青的性无能。当然，也表示自己生为毛家人，死为毛家鬼，最大的愿望就是为毛家留下一个后代。毛泽东何等聪明，马上明白了邵华的心意，龙颜大悦，便与邵华同房。就这样，毛新宇出生了。<br />
<br />
为毛新宇取名字时，毛泽东费了一番心思。本来应该用“岸”字辈，但不行，总不能叫毛岸宇，和毛岸青一辈吧？最后，他取了侄子毛远新的“新”字。“毛新宇”，意思很明白，毛远新、毛新宇，同辈之人，都是毛泽东的子侄辈。既让毛新宇名副其实的得到自己儿子的辈分，又能遮人耳目。</div>

							
							
							
							
