													<h2>毛新宇是毛泽东的孙子还是儿子?（转：有证据有推论）</h2>
						
						
																											<div id="postmessage_36178" class="t_msgfont"><a href="http://cache.qihoo.com/wenda.php?do=snap&amp;act=fetchHtmlsnap&amp;m=d2462897eaef8653399548045610723a&amp;surl=http%3A%2F%2Fmsn.myspace.cn%2Ft%2F3847960.html&amp;kw=%C3%AB%D0%C2%D3%EE%CA%C7%CB%AD%B5%C4%B6%F9%D7%D3%A3%BF" target="_blank">http://cache.qihoo.com/wenda.php ... 4%B6%F9%D7%D3%A3%BF</a><br />
<br />
近来，网民知情者贴出惊天秘闻，说：“毛新宇不是毛泽东的孙子，而是他的儿子”，引起了网民的激烈反应。大部分网友是相信这一消息的。我本想看一看邵华、 毛新宇的表态，因为对她们来说，他们太想否定这个消息了。而且，假如这个消息是“造谣”，那么，他们想揭穿这个“谣言”太简单了，只要做个基因鉴定，一公 布，谁还敢议论这事？但是，他们没这样做，不但没做，而且连个屁也不敢放，这说明什么？不敢吗！<br />
<br />
如果这仅仅是毛泽东、毛岸青、邵华 之间的下半身的事，我也不想说什么了。但不是。现在，毛新宇一直以毛泽东嫡孙的名义到处招摇撞骗，妄图利用毛泽东的僵尸捞取政治、经济、名誉、色情资本， 这可是坑害全国人民的事情，这就是个国家大事了。作为一个有良知的中国人，不能不把这件事情搞清楚。下面，我以中国公开发表的资料为证据，对这件事进行分 析。当然，中间难免有一些推论，就算抛砖引玉吧。<br />
<br />
一． 毛泽东多次讲过，他“绝后”了。<br />
<br />
<br />
毛泽东在卢 山会议上批彭德怀时，说彭德怀给他绝了后，他毛泽东“绝后”了。以后他又多次讲过。例如，他对康生说：始作俑者，其无后乎，我就绝了后。我们知道，毛泽东 封建意识很强，他不认为女儿是他的“后”，他认为儿子、孙子才是他的后。但毛岸英虽然死了，可还有儿子毛岸青，毛泽东怎么会说自己“绝后”了呢？显然，他 知道毛岸青不能生育，毛岸青生不了儿子，毛泽东就不可能有孙子，这不是“绝后”了吗？我们知道，中国人对自己的“后”非常重视，上至帝王，下至平民，无人 例外。毛泽东多次将这件事挂在嘴上，君无戏言，这可不是闹着玩的。毛泽东作为父亲，当然对自己的儿子毛岸青的生育能力了如指掌。从毛泽东亲口说的话里，我 们可以清楚的知道毛岸青没有生育能力。<br />
<br />
邵华毛新宇等人一直说，毛泽东是伟大的天才，一贯正确。那么，他关于自己“绝后”了的说法也 一定是正确的，这个预言也一定是天才的准确的。既然这样，怎么会冒出个孙子来呢？这个孙子是假的呢？还是毛泽东预言不正确？二者必有其一，请问邵华，既然 毛泽东的孙子是真的，那他关于“绝后”的说法就是错误的了，那你怎么说他一贯正确呢？既然他一贯正确，那你的那儿子就不是毛泽东的“后”，他是谁的后呢？<br />
<br />
二．毛岸青确实“有病”<br />
<br />
毛岸青的精神、肉体都“病”的很重，这是公开的秘密。有文章说毛岸青是精神分裂症，有人说 他是“傻子”，有人说他是“神经病”。他的身体也很虚弱，经常住院治疗。连和邵华结婚、都是在大连市医院里住院时搞的。这足以说明，毛岸青的身体状况很糟 糕。有人可能说，有病不一定就没有生育啊！可我们看一看毛岸青的婚恋史，他是个正常男人吗？<br />
<br />
毛岸青1923年11月2日出生， 1960年和邵华结婚时，他已经37岁了。此前，他没有婚恋过。试想，一个“皇子”，能拖到37岁结婚吗？如果他是“地富反坏右”的儿子，没人嫁他，拖到 37岁结婚，咱信。可他是“当今皇上”的“独子”啊！怎么解释？唯一的解释就是他“根本没有那个能力”。而且，毛岸青与邵华结婚，也是毛泽东亲自安排的儿 媳妇刘思齐撮合的。毛岸青根本不愿意谈恋爱。一个男人活到37岁了，见到像邵华这样的21岁漂亮姑娘送上门来都不动心，还要嫂子催，父亲劝。为了催毛岸青 娶邵华，毛泽东还亲自给毛岸青写了一封长信呢！如果毛岸青是个正常男人，毛泽东用得着这样吗？<br />
<br />
毛岸青与邵华1960年结婚。到1970年1月17日才生了毛新宇，十年啊！又是怎么回事？当时，谁不希望毛泽东添个孙子啊？邵华不想吗？怎么就是怀不上？这又说明，毛岸青没有生育能力。<br />
<br />
如 果在怀毛新宇前，毛岸青恢复了性功能，能生子了，那么邵华生了毛新宇后，为什么就停下不生了？那时可没搞什么“一胎化”啊，以后搞，也搞不到帝王家呀！请 邵华解释一下，如果毛岸青有生育能力，为什么不接着给“伟大领袖”再添个大孙子啊？邵华只生毛新宇一个，怎么那么巧，毛岸青早也没生育能力，晚也没生育能 力，单单怀毛新宇那一刻有了生育能力，而且一下子就生了个儿子，也太离奇了吧。事实说明，毛岸青没有生育能力。<br />
<br />
三、 从邵华的母亲、姐姐之品行度邵华之心思<br />
<br />
邵 华的母亲张文秋，先嫁给山东刘谦初，生女儿刘思齐又名刘松林。又嫁给陈振亚，生女儿邵华，前后不过几年。邵华本应随父姓陈，她却叫张少华，又改为邵华。中 国人很重视姓，哪能把姓改来改去？为什么不随父姓？是不是父亲不愿认这个女儿？为什么父亲不愿认？邵华的生父到底是谁？这只有她母亲张文秋知道了。<br />
<br />
邵华的姐姐刘思齐先嫁给毛岸英，毛岸英尸骨未寒，他就另嫁别人了。<br />
<br />
邵 华的母亲、姐姐都不是中国传统女人的类型。邵华的母亲张文秋是和我奶奶同龄的人。我爷爷被杀时，我奶奶才30多岁，但她没有改嫁，泪一把，汗一把，将我父 亲养大成人。受了多少欺凌啊，但她至死不改嫁，一直守寡到80多岁逝世。这才是那个时代的女人。近朱者赤，邵华出生在那样的家庭里，家庭所有成员都没有传 统女人的贞操，到邵华反倒有了？能守住一个神经病丈夫一辈子？且这个傻子还丧失了生育能力，傻瓜才相信这样的鬼话。<br />
<br />
四．从毛泽东的性史猜他与邵华的关系<br />
<br />
毛泽东“风流成性”。如果他是老百姓，则会被称为“流氓成性”，而且，他可能早就被以“流氓罪”判处死刑立即执行了。<br />
<br />
他十几岁就与本地姑娘结了婚。可他停妻再搞上了杨开慧。杨开慧还为他生了三个儿子，但她还没死呢，又搞上了贺子珍。贺子珍还没离呢，又搞上了江青，江青还年轻呢，他又搞了不知多少女人。直到临死80多岁的老翁，还霸着年轻貌美的张玉凤欲。<br />
<br />
这么个男人，守着如花似玉的儿媳妇，想着这个儿媳妇一直没有怀孕，看着自己的傻儿子呆呆坐在那里傻笑，他会干什么？君子度之可也。<br />
<br />
五．从江青对邵华的态度看毛泽东与邵华的关系<br />
<br />
江 青对邵华很不好，这是大家都清楚的。仅举一例，1966年7月26日，中央文革在北京大学召开万人大会，江青在会上发言，刚说了几句，就转向猛批邵华和她 的母亲，甚至说：“张文秋的女儿张少华，自己说她是毛主席的儿媳妇，我们根本不承认”（见陈伯达回忆录）。当时江青权利很大，一句话就会让人死啊，她为什 么对张文秋和张少华这么恨？江青自己没有儿子，用夺嫡之争没法解释，唯一的解释就是女人之间的争风吃醋。江青作为毛泽东的妻子，当然对毛泽东与邵华的关系 很关注。如果是正常的公公与儿媳的关系，婆婆会吃醋吗？会吃醋到想把她和她母亲整死的地步吗？<br />
<br />
话说回来，如果江青手里没有毛泽东与邵华私通的铁证，她敢公开这样做吗？毛泽东非打死她不可！但她这样做了，毛泽东一句话也不敢说，这为什么？明摆着吗。<br />
<br />
六．从邵华对毛泽东的吹捧看他们的关系<br />
<br />
邵华对毛泽东的吹捧，已到了肉麻的地步。请各位看一看她发表的文章，这哪里是儿媳妇夸公公啊！不是得到过切入骨髓之爱的人，怎么也不会说这种肉麻的话。 看一看毛泽东的女儿、女婿们，他们都说不出这种肉麻的话，唯独她邵华！这是为什么？难道邵华不知道毛泽东是个什么人？不知道他杀死多少人、害死多少人？不 知道他当面是人，背后是鬼？像邵华，都能爬到“将军”的人，难道糊涂到连这么点识别能力都没有？不可能吗！只有一种解释，“爱液”的作用呗！<br />
<br />
<br />
七．毛新宇到底是谁的儿子<br />
<br />
这个问题，只有邵华心里清楚，也可能连邵华也不清楚。</div>

							
							
							
							
