													<h2>迎世博 上海“六小工程”整治效果明显</h2>
						
						
																											<div id="postmessage_557161" class="t_msgfont">迎世博 上海“六小工程”整治效果明显<br />
2010-2-7 18:07:39<br />
&nbsp; &nbsp; 简要内容：狭窄的走道里横七竖八地停满自行车,是上海不少老小区楼道一“景”,居民进出磕磕碰碰,诸多不便。普陀区房管局副局长胡月华说,“六小工程”本来就是根据居民的呼声、根据基层街道的经验开始实施的。<br />
<br />
　　“世博就在身边。”--这是居住在上海市普陀区宜川街道赵家花园小区的忻月瑛的切身感受。因为世博,上海增添了新地标:雄浑大气的中国馆、飘逸灵动的演艺中心,忻月瑛很向往,做好了前去参观的计划;但她更在意的是:同样因为世博,她居住的老小区,一改垃圾乱扔、杂草丛生的脏乱环境。<br />
<br />
　　“老小区变了新模样,我住的房子成了景观房,住的老房子也升值了。”忻月瑛高兴地说。赵家花园小区的变化,源于世博大工程之外的普陀区的晾衣、停车、路面、绿化、椅子、门面“六小工程”。<br />
<br />
　　世博建设,有许多大工程。在大工程保质保量完成的同时,类似普陀区“六小工程”这样的城市整治、民生建设也在紧锣密鼓地进行。<br />
<br />
　　既要“面子”,更要“里子”<br />
<br />
　　自从2008年9月上海世博会筹办工作进入倒计时600天的关键阶段以来,上海每天都在发生变化,为了城市美观,不少小区“穿衣”、“戴帽”,让行人眼睛一亮,而行人看不到的,也在悄然发生变化。<br />
<br />
　　上海的老式小区很多,长期以来路面日久失修、楼梯没扶手、自行车在外面乱停乱放、邻里之间为抢占晾衣竿而闹起矛盾……这些看似微不足道的小细节、小问题,对城市居民生活质量影响可不小。<br />
<br />
　　从去年9月开始,普陀区通过广泛征求居民意见,开始了“六小工程”,下决心破解老式小区年久失修、公共空间相对狭小、停车乱、晒衣难、门面旧等难题。<br />
<br />
　　狭窄的走道里横七竖八地停满自行车,是上海不少老小区楼道一“景”,居民进出磕磕碰碰,诸多不便。<br />
<br />
　　为了解决辖区内桂杨园小区的这一“顽症”,曹杨街道办事处多次征询居民代表意见,根据小区的实际情况,专门请设计公司设计了双层的自行车停车架。这种目前在日本非常流行的双层停车架上下两层为一组,以液压为原理实现上下升降,既新颖美观,又方便使用,同时安全防盗,节约了很大的停车空间。<br />
<br />
　　除此之外,物业公司和居委会在制作停车地桩时也费了一番心思,经过研究讨论后,最终决定使用不锈钢螺旋形地桩,它比简单的横杆高了20多厘米,居民在停车时只要稍弯腰就能轻松地为车上锁,而原本车头部位的车锁设计也根据实际情况改在车尾部分,大大方便了居民锁车。<br />
<br />
　　“以前我们自行车没地方停,只能摆到楼道里,既麻烦又影响邻居。现在有了这样的停车架,大家都觉得非常方便,今后再也不用为停车而头疼了。”家住一楼的居民徐维高兴地说。<br />
<br />
　　群众再小的事,也是大事<br />
<br />
　　停车、晾衣……“六小工程”听起来都是些小事,但恰恰是这些小事,大得民心。<br />
<br />
　　例如晾衣服,“万国旗”曾是上海居住逼仄的一个缩影,因为晾衣服引发的邻里矛盾频频发生。如何做好“晾衣小工程”?政府部门在细节上动足了脑筋。在设计晾衣架尺寸时,根据许多身高不同的居民反复演示,最终确定的晾衣架最佳高度为1.9米,同时在做好的架子下方增设一层支架,让行动不便的老年人也能方便使用。<br />
<br />
　　在子长小区,已有111根高低不一的晾衣架,被放置在小区的不同位置,小区的居民们形象地把它们称作“高低杠”。<br />
<br />
　　甘宁街道城管科科长王正儒介绍说,晾衣架在已经完成的14个老小区试点时,都经过小区内详尽的日照时间评估,选择直面阳光照射、日照充足、远离主干道、远离障碍物的地址进行安装、施工。在晾衣架的下方铺设了植草砖;同时,考虑到晾衣架的整体美观性,还在晾衣架原有的基础上,增设了不锈钢晾衣竿、晾衣钩等,方便居民使用。<br />
<br />
　　如何把钱花在群众心坎上?<br />
<br />
　　“六小工程”实施后,普陀区居民幸福感直线上升。小区绿化改善后,许多居民自发组成了护绿队,不少人打消了搬迁的念头。居民奚美英说:“小区环境大变样,有人在医院住院,还专门坐出租车回来看,看着这样的环境,养病的心情也好多了。”<br />
<br />
　　普陀区委书记周国雄说,“六小工程”投资不算太大,每个小区平均不过几十万元,但得到了群众的衷心拥护,“钱花到了群众的心坎上。”<br />
<br />
　　钱如何花在群众心坎上?其实并不难,在“六小工程”实施过程中,普陀区在方案征集、策划等阶段让居民群众全过程参与,坚决办老百姓想办的实事、好事。<br />
<br />
　　樱花苑小区在实施“椅子工程”时,真如镇政府一口气拿出六个不同造型的休闲椅设计草图供居民挑选。居民代表们一眼相中了欧式铁质长椅。但是在试坐之后,不少居民认为椅子棱角太多,容易碰伤老人和孩子,提议更换成设计简约的实木椅。真如镇立即采纳,修改带有棱角的设计。喜欢在小区内晒太阳的潘老伯说:“当时我们还提出希望将休闲椅主要放在小河边和树阴下,这些建议都被采纳了。”<br />
<br />
　　普陀区房管局副局长胡月华说,“六小工程”本来就是根据居民的呼声、根据基层街道的经验开始实施的。<br />
<br />
　　“六小工程”实施至今,普陀区已有30个试点小区完成了改造,还有35个小区正在全力推进并将于春节前完成。2009年投入资金5000万元,2010年拟投入1.5亿元,再完成300个小区改造,平均每个小区将投入约50万元,让全区老小区都变个新模样。<br />
<br />
　　“如何最大限度地激发广大群众的自豪感和主人翁意识,使更多的市民认同世博、参与世博、服务世博?”周国雄介绍说,那就是通过工作创新,真正把世博筹办与改善群众的生活环境和提高生活质量结合起来,让群众切实感受到世博带来的城市变化,切实感受到世博会的主题“城市,让生活更美好。”<br />
