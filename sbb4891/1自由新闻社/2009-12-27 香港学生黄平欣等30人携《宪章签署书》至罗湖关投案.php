																											<div id="postmessage_401576" class="t_msgfont">水果报<br />
<br />
闯关网民尚未越过罗湖桥的边境界线，内地公安已擅自踏进本港境内抓走网民。本身是律师的立法会议员何俊仁批评，公安越境执法涉嫌违反一国两制原则及《基本法》，但两地政府可能视此为技术上的小问题，最终不了了之。<br />
<br />
