近日，坐落在加拿大温哥华卫星城列治文市的一座雕像“毛小姐试图在列宁头上站稳”，不断遭到破坏。这座雕像是温哥华公众艺术双年展的作品之一，由北京高氏兄弟创作。<br />
 <br />
温哥华湖南同乡会呼吁列治文市政府搬走该雕像，认为雕像丑化污损了毛泽东的形象，严重伤害了全体华人的感情。湖南同学会副主席胡先生，在出席中文电视台评论节目时表示，雕塑丑化了“中国人民的伟大领袖”，伤害了像他一样“热爱毛主席的中国人民的感情”，所以非常愤怒。<br />
 <br />
