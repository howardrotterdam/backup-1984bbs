																											<div id="postmessage_412774" class="t_msgfont">一座巨型的毛泽东小姐站在列宁头上的雕像在加拿大温哥华地区引发热议。有人认为雕像可以引发思考，有人则认为这座独裁者的雕像不应该矗立在市区。自由亚洲电台特约记者锡红从加拿大发来的报道<br />
<br />
加拿大温哥华的列治文地区矗立起一座毛泽东站立在巨型的列宁头像上的雕像。这是新一届的加拿大温哥华雕塑双年展的作品，由北京前卫艺术家高强、高滦值苤谱鳌<br />
<br />
雕像名为“毛小姐要在列宁头上站稳”，位于加拿大西岸城市温哥华卫星城烈治文的埃尔姆桥路（Elmbridge Way）夹尔德布里奇路（Alderbridge Way）交界处，雕像是一座闪烁耀目的银色金属巨型列宁头像，列宁的头像上站着一个小小的，手持一支平衡木，被女性及儿童化了的毛泽东的全身像。雕像高5 米，是温哥华地区2010年5月举办的公众艺术双年展作品。这件矗立在商业区的不锈钢作品，掀起了一番是表达自由，还是欠缺品味的争议。<br />
<br />
 一位市民说，我对这个雕像印象深刻，它太强烈了。<br />
<br />
 一位女士说，天哪，多么难看的东西。不过它看上去也挺有趣的。<br />
<br />
一位市民说，不，我不喜欢它。他们曾经是独裁者，把它放在这里是不合适的。<br />
<br />
一位妇女说，很有趣，它引发思考和讨论。艺术品就是要有这样的效果。<br />
<br />
列治文市华裔市议员邓伟雄认为，目前还没有必要将雕像移走。他说，如果这个东西实在让人厌恶无法容忍，我们就会要求把它移走。<br />
<br />
也有市民认为市区艺术作品应该有正面意义，而雕像展示的是两位独裁者，列宁领导的前苏共和毛泽东领导的中共两个政权，都要为许多人的非正常死难和不幸遭遇负责，因而以他们为主题的雕像非常不合适。但也有人认为，该作品只是对政治的嘲讽，是在取笑对政治偶像的崇拜。加拿大是个开放的社会，应该接纳不同构思和想法，也应该支持表达的自由。<br />
