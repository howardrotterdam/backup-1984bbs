 <br />
“加拿大价值守护者联盟”在向列治文市长发出的公开信中说，“由于中国大陆特殊的政体，很多新老移民即便在加拿大，对此类敏感话题也不愿意或者不敢公开发表意见，我们 ‘加拿大价值守护者联盟’愿意为沉默的大多数发声。”黄河边表示，毛泽东主政期间，对 8千万中国百姓的非正常死亡负有责任，在加拿大生活的华人应该对此有所反省。<br />
 <br />
公开信最后说，“我们认为，雕像展纯属公众的艺术活动，应该受到尊重和保护。加拿大价值守护者联盟期待列治文政府能够秉承多元包容的精神，让‘毛小姐企图在列宁头上站稳’的雕像继续展出。<br />
