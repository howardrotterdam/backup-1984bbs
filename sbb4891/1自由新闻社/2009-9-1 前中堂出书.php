																											<div id="postmessage_222044" class="t_msgfont">凤凰周刊独家：《朱F基答记者问》出版前后发表于 2009-08-31 22:41:27 <br />
退休6年后，朱F基以一本新书的出版回归公众视野，其意义已超过书籍本身，必将引发国人对其鲜明个性暨经济上的“朱F基时代”的切身记忆与情感涟漪。此书以朱F基多次接受媒体采访及演讲的记录为内容，是第一本真正意义上的朱F基著作。<br />
<br />
□ 凤凰周刊记者&nbsp;&nbsp;周宇&nbsp;&nbsp;特约撰稿员&nbsp;&nbsp;毕倩怡&nbsp;&nbsp;吴疆<br />
<br />
一本记录前国务院总理朱F基任职期间接受采访及演讲的新书即将面世。<br />
<br />
权威消息人士透露，截止本刊发稿时间，该书尚处于保密阶段，但相关部门已经批准该书9月出版。该书由人民出版社出版，封面是一幅朱F基讲话的经典照片，照片上朱F基身体微微前倾，稍稍抬起一只手。<br />
<br />
该书的出版工作高效而谨慎。人民出版社相关人士称：“该书是重大选题，涉及国家领导人，政治性很强，我们是举全社之力操作。”该书的发行量业内预期或将达到100万册。<br />
<br />
这是朱F基第一本真正意义上的个人著作，也是其退休6年来第一次正式出现在公众视野中。<br />
<br />
与退休后的平淡低调不同，朱在任时曾是一个“高调”的总理。每年“两会”期间，都会召开记者招待会，回答各国记者提问。朱F基风格的答问，曾经给国人留下深刻印象，媒体甚至称“就连北京最玩世不恭的出租车司机都激动得热泪盈眶”。<br />
<br />
此外，朱F基从担任副总理开始，就因其经济操盘手的特殊角色和鲜明个性，成为国外媒体关注的热门，曾经接受多家境外媒体专访，并在海外有多次演讲。<br />
<br />
但退休之后，朱F基很快从公众视野中消失，刻意保持低调。<br />
<br />
本港《亚洲周刊》报道称，朱F基退休后，多数时间住在北京和上海，有充分的时间读书，仍然享受国家领导人的待遇，并被中央要求加强了安保。<br />
<br />
大陆媒体报道称，朱F基描述自己的退休生活“也很丰富。我最喜爱的是中国的京剧，我现在不单是唱戏，而且还拉京胡，经过勤学苦练、名师指教，我现在拉胡琴的水平比过去提高了很多倍”。<br />
<br />
<br />
朱F基退休后仅有的几次公开露面包括：2004年8月，人民大会堂举行的邓小平诞辰100周年纪念大会；2004年12月，北京凯宾斯基饭店，德国中小企业联盟授予他“欧洲中小企业奖”的仪式。<br />
<br />
2006年4月，朱F基曾微服参观福州林则徐纪念馆。参观过程中朱F基虽神情沉静，但群众中不断有人要求与其合影，朱均未拒绝。<br />
<br />
朱F基走出纪念馆时，门外已经围满了听说朱F基在此参观的群众，并报以雷鸣般的掌声，朱F基亦绕座驾一周，向群众挥手示意。<br />
<br />
除此之外，朱F基拒绝回家乡长沙，拒绝阅读其堂兄为他写的传记，拒绝中华诗词协会邀请其担任名誉主席。朱F基的清华同学撰写、未经朱F基同意刻在湖南常德一面“诗墙”上的《槠基赞》也被悄悄拿下。<br />
<br />
作别6年之后，朱F基以一本新书回归公众视野，其意义已经超过了书籍内容本身，而令人联想到中国经济上为期长达12年的“朱F基时代”。睹书思人，必将引发国人对其人情感和记忆的涟漪。<br />
<br />
新书的低调面目<br />
<br />
即将出版的新书名为《朱F基答记者问》，主要内容为朱F基与媒体记者之间的访谈记录。<br />
<br />
相关人士透露，该书内容主要包括四大部分：朱F基担任总理5年间，每年“两会”期间记者招待会的答记者问；在境外访问期间的演讲；外国记者对朱F基的采访和专访；答香港记者问。<br />
<br />
朱F基“两会”期间的5次答记者问，留下众多经典语录，其中包括：<br />
<br />
1999年刚刚担任总理职务时，回答凤凰卫视中文台提问时的“不管前面是地雷阵还是万丈深渊，我都将一往无前，义无反顾，鞠躬尽瘁，死而后已”。<br />
<br />
2000年记者招待会上的：“我只希望在我卸任以后全国人民能说一句，他是一个清官，不是贪官，我就很满意了。那么再看开一点说，朱F基还是办一点实事，哎呀，我就谢天谢地！”<br />
<br />
2002年，人大会议闭幕的记者招待会上，朱F基回答记者提问时说：“从来不吓唬老百姓，只吓唬贪官污吏。”<br />
<br />
上述动情演说曾经为大陆民众所熟知。并有人专门撰写论文，将其作为演讲与口才的范本分析推许。<br />
<br />
但朱F基接受海外媒体的专访，以及在海外所作的演讲，对大陆民众来说尚显陌生。实际上，朱F基在海外与媒体和公众互动颇多，更加出彩。<br />
<br />
早在1985年，时任国家经贸委副主任的朱F基曾率领中国经济管理代表团访问美国、加拿大。朱F基此行先后在芝加哥世界贸易大会、加拿大麦吉尔大学、纽约联合国国际商会、哈佛大学、德克萨斯州立大学、旧金山商会等场合用英语发表9次演说，回答了听众100多个问题。<br />
<br />
1999年4月，身为国务院总理的朱F基出访美国。这次出访是朱F基与国外媒体互动和发表演说较为密集的一次。<br />
<br />
4月8日，朱F基与时任美国总统克林顿在白宫举行联合记者招待会。4月9日，接受美国公共电视台采访。4月13日，接受美国CNN主播朱迪・伍德拉夫（Judy Woodruff）专访。<br />
<br />
朱F基此行还在麻省理工学院、南加州、洛杉矶等地华人华侨欢迎晚宴等场合发表演讲。<br />
<br />
2000年7月，朱F基在比利时访问期间，应欧洲工业和雇主联合会的邀请，向欧洲企业和金融界人士发表演讲，重点介绍了中国的经济情况。2002年，朱F基在香港礼宾府、博鳌亚洲论坛年会等场合也曾发表演讲。<br />
<br />
此外，朱F基还曾接受过日本朝日新闻（2000年）、香港凤凰卫视（2002年）等境外媒体多次专访。<br />
<br />
上述专访和演讲的内容包括中国加入世贸组织、人权、改革、中美关系、对台政策等众多领域。<br />
<br />
与大陆读者看到的“洁版”不同，朱F基的海外演讲被公认为幽默、机智、有风度，给外国政要和海外华人都曾留下深刻印象。<br />
<br />
此次新书出版，以上内容或将得以呈现。新书保持低调面目：目前所见，朱F基没有为新书撰写文字，即使新书前言，也并非出自朱F基手笔，而是编者所加。通过如此安排，似乎在出书的同时，维持了卸任后不公开发言的一贯自律。<br />
<br />
海外出版焦点<br />
<br />
目前大陆尚无公开出版的朱F基传记。但与其他中共主要领导人一样，港台早有各种以朱为传主的纪实类著作流传。<br />
<br />
仅名为《朱F基传》的书籍就有台湾新新闻文化出版社版、台北时报文化出版社版、香港明报出版社版等多个版本。<br />
<br />
1999年，朱F基担任国务院总理之后，成为海外出版界炙手可热的题材。与以往的政治人物八卦书籍不同的是，朱F基被描述为颇为正面的智慧者，或是经济强人、管理者的形象。<br />
<br />
这一年，香港出版的有关朱F基的书籍包括镜报文化企业公司的《朱F基――雄才伟略》、香港夏菲尔国际出版公司的《朱F基大战走私集团》、香港皇冠出版社的《朱F基之逆境推销术》等。同年美国八方文化企业公司也出版了《朱F基新政――中国改革的新模式》。<br />
<br />
2001年，朱F基题材再次受到追捧。这一年，香港当代名家出版社出版了《朱F基的倾斜政策――从西部大开发到接班群热身》；香港新华彩印出版社出版《朱F基金融风暴》。明镜出版社则出版了《朱F基在1999》。<br />
<br />
《朱F基在1999》署名“宗海仁”，这被认为是化名，意为“来自中南海的人”。全书共9章，包括“春节前后、访美之际、处理FLG、驻南使馆被炸事件、应对两国论”等章节内容。<br />
<br />
日本历年来也在不断出版描写朱F基的书籍。包括1992年室井秀太郎所著《上海新世纪――朱F基和浦东开发》、1993年常恺所著《Jiang Zemin和朱F基――记者眼中的中国新领导人》、1998年朱建荣所著《朱F基的中国改革》以及1999年陈村金麦所著《改变了中国的朱F基的人脉和改革》，涵盖了朱F基作为国务院第一副总理以及国务院总理两个时期的起步阶段。<br />
<br />
此外，在中国生活了近30年的美国人龙安志（Laurence J.Brahm）也曾写过以朱F基为题的书籍：《朱F基与现代中国的变革》。<br />
<br />
龙安志在书中赞美朱F基“完全有资格获颁诺贝尔经济学奖，尽管这个奖向来是颁发给学者，而不是实践者的，（因为）学者们没有办法做到的，他都做到了”。龙安志认为朱F基在过去10年里，主导中国经济转型的成就之大，足以和凯恩斯相提并论。<br />
<br />
龙安志是朱F基的崇拜者。2001年，龙安志的新书《中国的世纪》一书出版。朱F基为其作序，并接见了他。<br />
<br />
大陆出版空白<br />
<br />
尽管龙安志毫不吝啬对朱F基的赞美，《朱F基与现代中国的变革》并未在中国大陆出版，而是2002年在新加坡出版了英文版。<br />
<br />
对于描写中共高层领导人的书籍，大陆向来管理严格。<br />
<br />
1990年5月5日，中共中央宣传部、新闻出版署联合发布了《关于对描写党国家主要领导人的出版物加强管理的规定》。<br />
<br />
《规定》界定了“党和国家主要领导人”的范围：现任或曾任党中央政治局的常委，国家主席、副主席，国务院总理，全国人大常务委员会委员长，中央顾问委员会主任，全国政协主席。<br />
<br />
《规定》要求专门描写、记述上述人物的专著、传记、回忆录、纪实文学、报告文学等必须符合《关于若干历史问题的决议》、《关于建国以来党的若干历史问题的决议》等中央有关文件的精神。<br />
<br />
有资格出版的单位仅限人民出版社、中央文献出版社、中共党史资料出版社、中共中央党校出版社、中国青年出版社、解放军出版社和各省、自治区、直辖市人民出版社，其他出版社一律不得参与。发行仅限新华书店。<br />
<br />
《规定》称，凡写健在的党和国家主要领导人的书稿，在报送新闻出版署之前，须先由出版单位征求本人意见。<br />
<br />
此前不久的1990年2月10日，新闻出版署刚刚发出《关于对描写中央主要领导同志的出版物复审和报批的通知》，要求对近年发表过和出版过相关出版物、作品的单位进行清理。<br />
<br />
朱F基本人显然并不看重那些关于他的传记。《上海文汇读书周报》2004年文章《卸任总理朱F基的淡泊生活》称，朱F基说他自己从来“不看别人写他的传记，认为都是东拼西凑去卖钱的”。<br />
<br />
朱F基的堂兄朱天池也曾写了一本《朱F基传》，2004年专程送到上海请朱F基过目，结果连朱F基的面都没见上。朱天池此后告诉大陆媒体，“暂时不想出版了”。<br />
<br />
多种原因，使得大陆至今没有出现专门记录和描写朱F基的出版物。<br />
<br />
但多本论述中国改革开放历程的书，均对朱F基时代的重大经济事件及其本人，作重点描述。其中包括《人民日报》作家凌志军的《变化――1990年―2002年中国实录》（中国社会科学出版社）、财经作家吴晓波的《激荡三十年》（中信出版社）等。<br />
<br />
最为大陆读者熟知的，则是2003年朱F基卸任之时，《南方周末》用24个版，多达30篇文章，赞美了朱F基的执政生涯。<br />
<br />
这组报道规格之高，不论是在大陆商业化媒体对国家领导人的报道中，还是在该报的历史上，都是史无前例的。<br />
<br />
这份报纸出版后迅速被抢购一空，显示了朱F基在民众心中占有相当地位。但有消息人士称，朱F基本人对此举并不欣赏。<br />
<br />
保密的新书<br />
<br />
清晰地描写朱F基，面临如许困难，令其在公众心目中始终保持神秘。剩下的渠道，或许只剩下朱F基自己出书了。<br />
<br />
大陆领导人出书，早已不是孤立事件。1980年代中期，薄一波出版的《建国以来若干重大事件的回顾》，被认为是领导人出书的肇始。<br />
<br />
进入2000年代，领导人出书变得频繁。2001年，杨尚昆在中央文献出版社出版了《杨尚昆日记》；李岚清则连续出版了《李岚清教育访谈录》（2003年12月，中国人民教育出版社）、《李岚清音乐笔谈》（2004年9月，高等教育出版社）两本。<br />
<br />
朱F基的前任LiPeng则出版了《众志绘宏图――LiPeng三峡日记》、《起步到发展――LiPeng核电日记》、《电力要先行――LiPeng电力日记》、《立法与监督――LiPeng人大日记》等日记体著作。<br />
<br />
2003年，钱其琛也出版了《外交十记》（世界知识出版社）。该书后来还出版了日文、英文等海外版本。<br />
<br />
事实上，在当代中国领导人中，朱F基本人其实著述颇丰，但均为事务性、学术性著作，几乎从不涉及个人。<br />
<br />
1980年代初期，朱F基曾经主编（或专著）了一批经济管理书籍。其中包括1980年天津人民出版社的《现代化管理》、1983年北京科学普及出版社的《管理现代化》、1984年天津人民出版社的《引进技术改造现有企业教程》以及1985年中国社会科学出版社的《当代中国的经济管理》。<br />
<br />
担任国务院总理之后，朱F基的专著包括1999、2000、2002、2003年，每年3月5日的《政府工作报告》，以及2001年3月5日的《关于国民经济和社会发展第十个五年计划纲要的报告》。上述专著均由人民出版社出版，同时出版的还有维吾尔语、哈萨克语等民族语言的版本。<br />
<br />
2001年新华音像中心出版社出版的录像制品《中国入世报告第一集：中国的机遇与挑战》，由朱F基等参与主讲。<br />
<br />
此番能有卸任多年的朱F基的新书出版，尽管并非对朱F基个人的描述，而更像是其经济思想的汇编，仍然令人期待。<br />
<br />
《南方周末》2006年曾刊载文章称，虽然不写传记，但朱F基对于任职期间一些重大决策的反思和回顾没有停止：2005年北京的一次清华老同学的聚会上，朱F基流露出准备系统整理成书的想法。<br />
<br />
与朱F基之前的重要专著一样，新书依然由人民出版社出版。但该书的运作始终处于保密之中。<br />
<br />
人民出版社负责宣传的官员一度坚称毫不知晓此书的出版计划。匿名消息源称，该书原定8月中旬印刷，但因为运作的慎重，以及审查之需要，推迟为9月份面世。新书的宣传工作尚未完全展开。<br />
<br />
对于该书出版的动因和背景，上述人民出版社宣传官员称：“由上面考虑，我们不太清楚。”<br />
<br />
全部相关内容，详细见近期出版的凤凰周刊杂志</div>

							
							
							
							
