																											<div id="postmessage_839203" class="t_msgfont">原文出处：<a href="http://www.chinaelections.org/NewsInfo.asp?NewsID=187437" target="_blank">http://www.chinaelections.org/NewsInfo.asp?NewsID=187437</a><br />
<br />
钓鱼岛一开局中国就掉进了日本的圈套<br />
作者：180°时差<br />
来源：万维读者网<br />
来源日期：2010-9-20<br />
本站发布时间：2010-9-20 10:21:28<br />
阅读量：1339次<br />
<br />
　　日本人已经习惯于这一手了，先做好一个圈套，而后制造一个借口，让对手钻进来。几十年过去了，日本人仍旧没有长进，仍旧在玩如此低劣的小把戏。遗憾的是中国人竟然仍旧不长记性，还是跟着他们转，跟着他们的先手走棋。<br />
<br />
　　此次诬赖中国渔船冲撞他们的巡逻艇，和当年七七卢沟桥事变如出一辙。日本人自己制造出一个借口：有一名日军士兵失踪，于是在1937年7月7日夜，径自在中国驻军阵地附近举行所谓军事演习，进而集结军队，由此一举开始了全面占领中国的战争。在日本军队已经打算全面进攻之际，当时愚蠢的冀察当局还在考虑不让事态扩大，与日方商议，同意协同派员前往卢沟桥调查失踪的日军士兵。直到7月8日晨5时左右日军大炮的轰炸声才把天真的中国人炸醒。假如当年诸葛亮在那里，至少可以让过卢沟桥的日本人尝尝“火烧赤壁”一样的苦头。他们在那里结集的时候，有足够的时间搬来汽油桶，要比火烧赤壁容易得多，可惜的是中国人只会跟着日本人的先手下棋，甘当后手，这是没有办法的事。<br />
<br />
　　如今，又是一个天大的奇闻，哪有小吨位的渔船主动去撞大吨位的巡逻船的？日本的巡逻船有89米长，中国的破渔船只有37米长。以小撞大？中国船长不是在开自己性命的玩笑？谁会用一把土枪面对面地去打一挺机关枪？那位船长去那里是为了捕鱼，不是去当董存瑞的，一旦翻船，自己只能喂鲨鱼，难道船长连这点常识都没有？日本编造谎言也应该编的高档一点。其实一上来，日本人就痛下了黑手，自己撞了渔船，然后把中国渔船一把抓住，倒打一耙，来欺骗国际舆论，来影响视听。现代日本人仍旧不改当年日本浪人无赖的本性。<br />
<br />
　　还是“胡子”出生的张作霖能看穿日本人玩的把戏。一次他受邀出席日本人的酒会。席上，一个日本人请张大帅赏字，他知道张作霖是土匪出生，粗人一个，肚里的墨水少，想当众出他的洋相。没想到张大帅枪不让人，笔也不肯让人。提笔写了一个“虎”字，虎字生威，显示出他不让人的豪气，然后落款 “张作霖手黑”五个字。那个日本人看到后笑出声来，便凑近大帅讥笑道：“大帅写的‘手墨’的‘墨’字，下面少了个‘土’成了‘黑’了。”不料张作霖一瞪眼睛骂道：“妈了巴子！我还不知道‘墨’字怎样写吗？对付日本人，手不黑，行吗？这叫‘寸土不让！’”在场的日本人听得都目瞪口呆。这就是张作霖“寸土不让”的故事。不过张作霖土匪的黑手还是黑不过日本人，你想寸土不让，就让你尸骨难找，在皇姑屯把张作霖炸死一事可证明日本人做事：一来是很绝的，二来是不达目的决不罢休的。<br />
<br />
　　这次中国人“墨”守成规，没有 “黑” 守成规。日本人在下黑子，中国人在下白子，黑白是走不到一起的。又失去了先手，接下来通过外交途径一次次地喊话，更是一错再错，你喊一次就是在随着日本人的先手下一粒子。即便现在上街游行，也是在跟着落子。但有两步棋是走对的，直接向春晓油田运钻井器材和宣布推迟中日东海谈判，你走你的棋，我走我的棋，如果到最后日本人停下自己的思路不得不跟着中国人走棋了，那就是抢回了先手，胜利才有希望。<br />
<br />
　　这次日本人来钓鱼岛摆下棋局，表面上是由于日本政客为了拉选票而为之，其实不然，他们的目的一是为了在东海谈判中增加筹码，二是在为了国际化，让全世界知道：是中国渔船撞船在先，日本不得不依法从事。洋人不明真相，很容易联想到自己的汽车被撞，撞得人肯定有问题，依法从事是对的。日本人以一个先入为主的习惯想法偷换了一个领土归属的概念。一旦船长被判，等于建立一个判例，其根本目的是为了推翻日本著名历史学家井上清“中国是最早发现钓鱼岛并且行使有效管辖的国家，钓鱼岛自古以来就是中国的领土”的结论和钓鱼岛属于中国固有领土的历史事实。<br />
<br />
　　日本人选择的时机是经过精心设计的，在刚刚扣押中国渔船、渔民和船长时，他们可以用选举目的来蒙骗中国和减少压力，中国实际上也上了当，还以为按照以往的老规矩，用一些老掉牙的老三篇，对着各路记者讲一通，日本人就会放人，就会像以往一样马上雨过天晴。再晚到了选举结束，事情也该落幕了。日本人也借中国人在判断上的失误而赢得了时间，中间把船员和渔船放回来，不过是做做姿态，他们需要的是时间。一旦有了时间，他们就迫不及待地直奔主题。他们设计中的关键一环就是要把一个中国公民送上日本的法庭，而且这件CASE的卷宗里必然会有三个字“钓鱼岛”。日本人沙盘推演了很久，就是为了有一个历史记录：中国公民因“非法”进入日本领土“钓鱼岛”而被日本的法院判有罪。<br />
<br />
