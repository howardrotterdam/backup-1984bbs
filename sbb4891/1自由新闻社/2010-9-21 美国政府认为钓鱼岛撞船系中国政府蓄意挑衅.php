&quot; 要冷静，不要到前线去充当抗日英雄，要避开与日本的正面冲突，绕到日军后方去打游击，要想办法扩充八路军、建立抗日游击根据地，要千方百计地积蓄和壮大我党的武装力量。对政府方面催促的开赴前线的命令，要以各种借口予以推拖，只有在日军大大杀伤国军之后，我们才能坐收抗日成果，去夺取国民党的政权。我们中国共产党人一定要趁着国民党与日本人拼命撕杀的天赐良机，一定要趁着日本占领中国的大好时机全力壮大，发展自己，一定要抗日胜利后，打败精疲力尽的国民党，拿下整个中国。&quot; <br />
有的人认为我们应该多抗日，才爱国，但那爱的是蒋介石的国，我们中国共产党人的祖国是全世界共产党人共同的祖国即苏维埃（苏联）。我们共产党人的方针是，要让日本军队多占地，形成蒋、日、我，三国志，这样的形势对我们才有利，最糟糕的情况不过是日本人占领了全中国，到时候我也还可以借助苏联的力量打回来嘛！ <br />
