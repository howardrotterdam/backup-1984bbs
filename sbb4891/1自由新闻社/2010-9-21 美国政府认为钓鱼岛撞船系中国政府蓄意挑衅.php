正好一大臣前来求赐国名，大郎随囗道：“日本鬼子。”另一大臣拿出白布求画国旗。大郎随手摸出一块炊饼大力拍去，想到西门庆，羞怒交加，喷出一口鲜血在炊饼上。就成了日本的国旗。大郎又想，“西门庆和潘金莲想害死我，然而我的运气不错，挺长久。”立即下诏，在国旗上写：武运长久。意思就是：武大郎运气长久。又想起当初对大乌龟的承诺，下令全国视乌龟为神物。大郎是卖炊饼出身，见人就大鞠躬为本色。全日本见国王如此，无不仿效。<br />
<br />
武大郎作为国王，开头还相当勤勉。每天都是“有事出班早奏，无事早早退朝”。但过了一些日子，他觉得很没劲。官员们鸡毛蒜皮的事都要讲大半天。于是他说：“你们以后把事情的重要内容写成奏折，交给我看。”大臣们很惊奇的说：“什么叫做写？我们不识字，不会写。”武大郎说：“好吧，我给大家办个补习班，扫扫盲。”于是武大郎凭自己有限的知识给大臣门开了个扫盲班，学习文字。但武大郎是个卖炊饼的人，只认识很少的字，很多字他只记得一些边旁部首，大臣们在学习及往外传播的时候，又忘了一部分字，于是这就是形成一种“假”文字，平假名、片假名之类。这是东洋文化的重要组成部分。<br />
<br />
有一天，大郎发现臣民没有姓。于是他又说：“这可不行，得有名有姓才行。这样吧，你们住在哪里就姓什么吧。”于是就有了“田中”、“松下”、“山口”之类的姓氏。至于名字，就一二三四五六七的排吧。但老大不能叫大郎，那是我的忌讳，只能叫太郎；老二不能叫二郎，那是我弟弟武松武二郎的忌讳，只能叫次郎，其余的你们就按顺序叫吧，我没有意见。”于是这个国家就有了山口太郎，田中次郎、山本五十六等等名字。<br />
<br />
武大郎当了国王后，老是吃山珍海味，吃都吃腻了。他想起当初在海上漂流时，没有东西吃，只能捉生鱼吃，现在回想起来那味道还是相当不错，于是他叫厨师做鱼时一定要生做，不用做熟。这道菜推广开来，得到人们的欢迎，从此成为该国的一大名菜“生鱼片”。武大郎还发现，他们象中国人一样，睡觉都睡在床上。他很生气，想当初潘金莲和西门庆搞婚外情，西门庆经常到自己家里，搞到自己没有地方睡，只好睡在地上。既然我当国王的都要睡在地上，你们也只能睡在地上！这样子才能不忘夺妻的耻辱！于是他按照这个意思又颁布了一项法令，从此以后他们只能睡在一张席子上，这就是所谓的“塌塌米”。<br />
<br />
武大郎在中原时被人称作“三寸丁”，有自卑感，他在日本怕被人看扁。便下令全国比自己高的男子都处死。很多家庭为了让儿子不死，将儿子打成罗圈腿。所以日本鬼子矮而且罗圈腿多。大郎因为潘金莲而痛恨女人，下令日本女子只能在家伺候丈夫，而且要跪着伺候，已示惩罚。漂亮的女子全部处死，所以日本的丑女极多。<br />
<br />
武大郎当了若干年的国王，无疾而终。他临死之际，仍然为打不过西门庆、报不了夺妻之仇而耿耿于怀，于是留下遗训，要子孙后代找西门庆报仇雪恨。后来他的子孙们日夜操练，跑到少林寺偷学了几招功夫，为了纪念国王武大郎，取名“武氏道”，后来因为日本鬼子的文化程度低，加上该国的文字是假文字，结果被传成了“武士道”。因为武大郎白手得天下，这些功夫又被称作“空手道”。<br />
<br />
到了明朝，武大郎的后人便开始派人登上中国领土，寻找西门庆报仇，被中国的英雄戚继光赶下了海，那就是历史上的“抗倭”。进入了二十世纪，日本鬼子在中国自北向南，自东向西跑遍了中国大半河山，还是没有寻到仇人西门庆。于是他们居然要中国人学习他们的“假文字”，要中国人取他们那样的名字，要中国人在“炊饼旗子”下面实行“大东亚共荣”。这真是让中国人笑掉了大牙。<br />
<br />
最近，武大郎的后人据说有可靠情报，怀疑西门庆躲藏在台湾一带，于是在台湾对面的钓鱼岛，好像整天有人在那里卖炊饼了。</div>

							
							
							
							
