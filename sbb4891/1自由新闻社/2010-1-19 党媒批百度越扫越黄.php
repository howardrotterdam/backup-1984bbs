<br />
&nbsp; &nbsp;&nbsp; &nbsp; 人民网北京1月18日讯(记者崔雷) 记者今日调查发现，百度图片搜索中仍存在极为淫秽的图片；其“老年搜索”相关搜索页面中，也存在淫秽色情信息；其“政府网站搜索”可以搜到黄色网站。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;“护士美眉”很黄很暴力“政府搜索”能搜到黄网<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 2009年12月3日，人民网曾对百度搜索引擎对关键词处理不当的事情进行报道。根据网友的投诉，记者当时发现，在百度中搜索“护士美眉”关键词，搜索结果中有色情网站的链接。此事经报道后，记者看到，百度对相应的搜索结果进行了处理。不过，时过一个半月，当记者再次用此关键词搜索时，仍然在百度图片中搜到赤裸裸的成人漫画，其内容比此前报道的更加不堪入目。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 在百度的另一个搜索引擎产品――“政府网站搜索”中，记者同样输入“护士美眉”这个关键词，得到的第一条搜索结果竟然是一个黄色网站的地址，点击该链接后，电脑中弹出大量淫秽不堪的网页。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; “少儿搜索”被迫关闭“老年搜索”仍存色情<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 前段时间，中央电视台报道了“百度少儿搜索”中存在大量淫秽色情的信息。为此，百度被迫关闭了该功能。记者发现，在百度产品大全中已经找不到该产品。然而，记者在百度为中老年用户设计和开发的“老年搜索”中却发现了一些曾经被曝光、至今未被屏蔽掉的淫秽内容。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 有媒体曾报道，在“百度少儿搜索”中用“激情”、“视频”、“聊天”几个关键词单组合，便可以搜索到大量黄色网站的链接地址。记者在“百度老年搜索”试了试。当记者将这三个关键字两个一组输入搜索框进行搜索时，发现搜索结果的最上方显示有“根据相关法律法规和政策，部分搜索结果未予显示。”的提示，页面中很难发现黄色网站的链接。但当记者将这三个关键字同时输入后，却得到了不同的结果：在搜索结果中出现了大量“美女激情聊天”、“火辣美女”，甚至“成人”、“裸聊”等字样的链接。随便进入其中一个，不难发现这些网站很多都存在低俗淫秽的内容。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 互联网仍是扫黄重点搜索引擎需加强监管<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 打击搜索引擎“传黄”问题早在2009年年初就成为“整治网络低俗”专项行动的工作重点，在一年多的多次专项行动中，搜索引擎企业经常被相关部门点名警告。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 向全国青少年发出“抵制手机网络色情”倡议的北京师范大学第二附属中学的方策同学在接受人民网访谈时曾表示，目前中学生搜索淫秽信息的最主要渠道依然是搜索引擎。而近期公布的《2009年中国搜索引擎市场研究专题报道》显示，在中国搜索引擎市场，百度占据绝对优势，有63%的用户习惯使用百度。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 据中新社报道，本月15日，中共中央政治局委员、中央书记处书记、中宣部部长刘云山在部署2010年“扫黄打非”工作会议中强调，“扫黄打非”工作要坚定不移地保护知识产权，坚决打击各类非法出版活动，坚决清除淫秽色情等文化垃圾，促进未成年人身心健康，促进社会主义文化繁荣发展，为全面建设小康社会营造良好社会文化环境。<br />
