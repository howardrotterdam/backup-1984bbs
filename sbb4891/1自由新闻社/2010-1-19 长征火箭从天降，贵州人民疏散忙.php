2010-01-18 05:31:00　来源: 中国经济网(北京)<br />
<br />
核心提示：昨日凌晨，搭载我国第三颗北斗导航卫星的长征三号丙运载火箭，在西昌卫星发射中心点火升空7分钟后，其一级火箭助推器成功分离，随后坠落至仁怀市九仓镇山区。当地出动2000多名民兵和警察、医疗救护人员等，组织疏散了落区的10多万群众。<br />
<br />
2010年1月17日，北斗导航卫星火箭发动机部份残骸在贵州省黔东南州镇远县大地乡一小山村附近溅落。<br />
<br />
中国经济网1月18日报道 昨日凌晨，搭载我国第三颗北斗导航卫星的长征三号丙运载火箭，在西昌卫星发射中心点火升空7分钟后，其一级火箭助推器成功分离，随后坠落至仁怀市九仓镇山区。<br />
<br />
九仓镇村民刘运金目睹了残骸坠落的过程，他说，凌晨零点20分左右，天空中传来一阵沉闷的爆炸声，两个快速移动的火球划出弧线，急速掠过头顶。“很快就听到两声巨响，地面和身边的树枝都在抖动。”刘运金说，其中一个火球坠落在他所站位置对面的山坡上，火球坠地瞬间，腾起冲天火光，数十秒后逐渐熄灭。<br />
<br />
据介绍，火箭助推器残骸是个白色圆筒（上图），长约10米，直径约2米，助推器残骸坠地时的热浪，使60米直径范围内的草和树木枯萎；坠地产生的巨大冲击力，在地面上砸出了大坑，所幸未造成人员及财产损失。<br />
<br />
据悉，仁怀市九仓等4个乡镇，是此次火箭发射任务的残骸理论溅落区。当地出动2000多名民兵和警察、医疗救护人员等，于1月16日傍晚，组织疏散了落区的10多万群众，并做好应急准备。目前，当地已对火箭助推器残骸进行严密保护，等待有关方面回收。（贵阳晚报）<br />
<br />
