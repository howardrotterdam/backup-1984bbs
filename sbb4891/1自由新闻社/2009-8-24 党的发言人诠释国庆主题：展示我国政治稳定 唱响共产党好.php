<br />
9月30日晚，国务院将在人民大会堂举行盛大国庆招待会。<br />
<br />
10月1日上午，将在北京天安门广场举行首都各界庆祝中华人民共和国成立60周年大会，中共中央总书记、国家主席、中央军委主席Hu Jintao发表重要讲话，之后举行盛大的阅兵式和群众游行。阅兵将重点展示新中国成立60年来特别是改革开放30年来国防和军队建设的成果，显示我维护世界和地区和平与稳定的坚定决心和意志，展示人民军队威武之师、文明之师、和平之师的良好精神风貌。群众游行以“我与祖国共奋进”为主题，共设“奋斗创业”“改革开放”“世纪跨越”“科学发展”“辉煌成就”“锦绣中华”和“美好未来”等7个部分，由近20万群众和60辆彩车组成。<br />
