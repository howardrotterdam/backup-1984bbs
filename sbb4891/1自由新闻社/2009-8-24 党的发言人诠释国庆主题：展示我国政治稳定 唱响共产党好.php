<br />
10月1日至3日，在北京各区县主要公园和公共绿地，将举行国庆游园活动。<br />
<br />
9月中下旬，将在北京展览馆举办“辉煌六十年――中华人民共和国成立60周年成就展”。9月下旬，将在人民大会堂演出集舞蹈、声乐、器乐、话剧、朗诵、戏曲、多媒体等多种艺术形式于一体的大型音乐舞蹈史诗《复兴之路》，展现中华民族争取国家独立、民族解放的历史，记录探索救国真理和强国之路的历程，展示当代中国改革开放和现代化建设的辉煌成就和美好前景。 <br />
