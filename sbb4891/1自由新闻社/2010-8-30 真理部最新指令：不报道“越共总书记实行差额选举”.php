																											<div id="postmessage_805421" class="t_msgfont">差额选举，是指候选人名额多于应选代表名额的选举（区别于‘等额选举’），我国从1979年选举法开始变等额选举为差额选举。 差额选举：直接选举的，代表候选人名额应多于应选代表名额的三分之一至一倍；间接选举的，代表候选人的名额应多于应选代表名额的五分之一至二分之一。 　　所谓差额选举，就是在选举中实行候选人数多于应选名额的选举。 　　根据《中国共产党地方组织选举工作条例》和《中国共产党基层组织选举工作暂行条例》的规定，就党的地方组织和党的基层组织而言，选举产生以下党内职务要实行差额选举： 　　（1）党的地方各级代表大会代表，党的基层代表大会代表，党的地方各级代表会议需经选举产生的代表； 　　（2）党的地方各级委员会委员、候补委员，党的地方各级纪律检查委员会委员； 　　（3）党的基层委员会、总支部委员会、支部委员会委员和纪律检查委员会委员； 　　（4）党的地方各级委员会常务委员会委员，党的基层委员会常务委员会委员，党的地方各级纪律检查委员会和基层纪律检查委员会常务委员会委员。 　　差额选举有两种方式： 　　直接采用候选人数多于应选人数的差额选举办法进行正式选举； 　　先采用差额选举办法进行预选，产生候选人名单，然后进行正式选举。<a href="http://baike.baidu.com/view/239485.htm?fr=ala0_1_1" target="_blank">http://baike.baidu.com/view/239485.htm?fr=ala0_1_1</a></div>

							
							
							
							
