													<h2>法国蒙彼利埃市将为列宁毛泽东等树碑立像</h2>
						
						
																											<div id="postmessage_788148" class="t_msgfont">中新社巴黎8月18日电 法国南部城市蒙彼利埃将在该市树立十尊名为“20世纪伟人”的雕像，其中包括新中国缔造者毛泽东和世界上第一个社会主义国家的创建者列宁。<br />
<br />
蒙彼利埃市18日在该市东部安装了已经完工的五尊雕像，分别是列宁、英国前首相丘吉尔、美国前总统罗斯福、法国前总统戴高乐和法国前社会党领袖饶勒斯。这些青铜雕像高3.3米，重量在850公斤到1吨之间，每尊造价20万欧元，计划在今年九月中旬正式揭幕。<br />
<br />
除以上五尊外，蒙彼利埃市还计划树立毛泽东、印度国父甘地、以色列第1位女总理梅厄、南非前总统曼德拉以及埃及前领导人纳赛尔的雕像，这些雕像预计在2011年底前完工。<br />
<br />
为“20世纪伟人”树碑立像的倡议是由蒙彼利埃市前市长、法国朗格多克-鲁西永地区议会主席乔治・弗雷切提出的，目的是“让青年人在这些雕像前揣摩这些历史伟人的政治思想”。<br />
<br />
在谈到为毛泽东立像时，乔治・弗雷切表示，是因为“毛泽东为中国重树了尊严”；至于列宁，乔治・弗雷切则说，正是列宁改变了20世纪的人类世界。列宁一生有两个光辉的时刻：一个是1917年的十月革命，其次就是列宁在国际社会上第一个提出民族自决，提倡非殖民化。乔治・弗雷切认为，1917年十月革命的历史改变了世界。如果没有1917年，就不会有非洲、印度和中国的非殖民化，也就不会有今天整个世界的发展。<br />
<br />
据介绍，乔治・弗雷切早前还曾表示希望为前苏联领导人斯大林树立雕像，但由于争议太大而作罢。<br />
<br />
<br />
(本文来源：中国新闻网 作者：吴卫中)</div>

							
							
							
							
