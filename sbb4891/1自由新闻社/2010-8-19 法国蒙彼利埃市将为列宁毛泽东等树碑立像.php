　　蒙彼利埃市18日在该市东部安装了已经完工的五尊雕像，分别是列宁、英国前首相丘吉尔、美国前总统罗斯福、法国前总统戴高乐和法国前社会党领袖饶勒斯。这些青铜雕像高3.3米，重量在850公斤到1吨之间，每尊造价20万欧元，计划在今年九月中旬正式揭幕。<br />
<br />
　　除以上五尊外，蒙彼利埃市还计划树立毛泽东、印度国父甘地、以色列第1位女总理梅厄、南非前总统曼德拉以及埃及前领导人纳赛尔的雕像，这些雕像预计在2011年底前完工。<br />
<br />
<br />
