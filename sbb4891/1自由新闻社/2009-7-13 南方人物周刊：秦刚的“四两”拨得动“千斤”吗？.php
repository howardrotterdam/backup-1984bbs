													<h2>南方人物周刊：秦刚的“四两”拨得动“千斤”吗？</h2>
						
						
																											<div id="postmessage_160987" class="t_msgfont">作者: 黄广明 <br />
本文网址：<a href="http://www.infzm.com/content/31365" target="_blank">http://www.infzm.com/content/31365</a><br />
名人大讲堂<br />
不是名人上课，是给名人上课<br />
<br />
在中国外交部发言人中，秦刚先生算一个异数，他不但经常发布新闻，还经常把自己也整成了新闻当事人，个中原因，即在于他在回答记者提问时每每有惊人之语。兹举两例――<br />
去年10月14日，有记者问10月17日之后，《北京奥运会及其筹备期间外国记者在华采访规定》（因为该规定大大提升了外国记者在中国采访的自由度，所以颇受好评）是否延续下去或者会被替换掉，秦刚答道，“你手表上有日历吗？请你看看今天是几号。”<br />
今年6月9日，在回答记者关于绿坝的问题时，他反问记者：“你有孩子吗？”记者无言，接下来秦刚说，“如果你有孩子或者你打算要孩子，你应该能理解作为家长对网际网络传播有害信息的关切。”<br />
……综观秦先生在重大问题、富有挑战性问题上的回答风格，都有点“四两拨千斤”的感觉，记者很严肃，问题很尖锐，然而，都被秦刚先生一两句简短的反问、讥讽或者俏皮话挡将回去。以至于有人对秦先生推崇有加，说他幽默、机智，同时又大力维护了国家尊严，实乃不可多得的人才。<br />
不过，笔者观感却不大相同，秦刚先生确实发出了“四两”神功，然而，他的“四两”真的拨动了“千斤”吗？换言之，他的“幽默”、“讥讽”、俏皮话真是回答问题的点睛之笔，还是逃避正面回答的遁词？抑或文不对题的秀幽默、秀讥讽？<br />
譬如，BBC记者的关于电脑预装“绿坝”的提问，记者关心的是这个软件会否屏蔽一些网站，而秦刚反问记者一句，“你有孩子吗？”这个意思很清楚，就 是这款软件会屏蔽一些色情网站，会保护孩子。而记者提问的真正意思是是否会以保护孩子之名扩大化屏蔽掉网站，损害公民自由获取信息的权利，这与孩子不孩子 是没太大关系的，于是，“你有孩子吗”这句反问，虽然貌似机警，却有些驴唇不对马嘴了。此其为“四两”与“千斤”中逻辑上的矛盾。<br />
看来，秦刚先生的四两并未拨动千斤，相反给了人不正面回答问题的印象。<br />
