													<h2>lz莫非说是这个圣贴?</h2>
						
						
																											<div id="postmessage_391966" class="t_msgfont">jinjq02 (future in my hand @ New York City) 于&nbsp;&nbsp;(Sun Dec 20 16:10:48 2009)&nbsp;&nbsp;提到:<br />
<br />
<br />
今天也不知道怎么了&nbsp;&nbsp;突然想回来水木看看&nbsp; &nbsp;在之前的几年里面&nbsp;&nbsp;偶尔会回来&nbsp; &nbsp;但是每次都感觉&nbsp;&nbsp;国内很多东西离我好远&nbsp; &nbsp;也没有什么好给comment的地方<br />
<br />
不过想想在美国这么多年&nbsp;&nbsp;总共回国两次&nbsp; &nbsp;每次回到清华&nbsp;&nbsp;回到理学院&nbsp; &nbsp;阮东&nbsp;&nbsp;吴念乐他们都还是对我很好&nbsp; &nbsp;这都让我想起了以前那3年的清华时光&nbsp; &nbsp;记得去年在科学馆吴老师和我说了好多好多&nbsp; &nbsp;让我看到的理9祁晓亮他们的成绩&nbsp; &nbsp;也让我觉得也许做research 是我更合适的方向吧<br />
<br />
Attached document是06年底写下的&nbsp; &nbsp;现在看看&nbsp;&nbsp;已经时过境迁&nbsp; &nbsp;很有覆巢之下无完卵的感觉&nbsp;&nbsp;但是多少算是一个从non-finance领域转wall st.的例子吧&nbsp;&nbsp;有兴趣的可以看看<br />
<br />
这个小总结&nbsp; &nbsp;就从07年开始说说&nbsp;&nbsp;但愿对考虑出国&nbsp;&nbsp;面对职场选择&nbsp;&nbsp;想要做research的人起到一些抛砖引玉的作用吧<br />
<br />
1) Mortgage Research <br />
<br />
07 年6月开始进入quant trading program at Citigroup. Program招的基本都是top school undergrads/master students. 这个quant trading program是个rotation program. 第一年我被分到mortgage research and analysis这一组.&nbsp;&nbsp;现在想想&nbsp;&nbsp;我的那些manager对我都还是很好的&nbsp; &nbsp;他们有很强的实践能力&nbsp;&nbsp;又有很强的独立科研能力<br />
<br />
Mortgage Research里面一大部分是model prepayment rate. 在financial crisis下面一度voluntary prepayment (across origination years)被压得很低&nbsp; &nbsp;同时government intervention (i.e. FNMA/FHLMC bailout etc.) 对future projection添加了很多uncertainties. 同时housing data的获取和aggregation一直都是比较tricky和detail-oriented的. 很多时候CPSCDS的data是不够的。 所以这个领域&nbsp; &nbsp;就算是最top experts 也要每天学习很多新的知识&nbsp;&nbsp;&amp; work under big pressure. <br />
<br />
而我更多involve的是model default, cumulative losses,&nbsp;&nbsp;and understand loss severity. 这块因为房市崩盘而变得尤为重要&nbsp;&nbsp;整体觉得mortgage research是很锻炼人的地方&nbsp;&nbsp;有很多ground work&nbsp; &nbsp;虽然很多时候很辛苦&nbsp;&nbsp;但是如果不从下到上的了解一个model 又怎么能够真的懂得这个系统呢&nbsp; &nbsp;&nbsp;&nbsp;那一年&nbsp;&nbsp;经常要苦战的深夜&nbsp;&nbsp;从Bear Sterns的两个hedge fund collapse之后&nbsp; &nbsp;整个housing bubble彻底破灭&nbsp; &nbsp;看到金融危机对各个层面的影响&nbsp;&nbsp;也是一个不一样的经历。<br />
<br />
2) Fixed-Income Structured Trading<br />
<br />
第二年的trading rotation在structured trading desk. 我发现trading这个行业真的很有意思&nbsp;&nbsp;整体来讲我觉得也是比较适合我的personality。 Trading desk分的很细致&nbsp;&nbsp;比如mortgage-related的 IO/PO traders 和CDO traders都是各做各的东西&nbsp;&nbsp;但是different market components are inter-related. 所以其实很复杂。当每个行为都和钱打交道的时候， 责任和压力也很大。<br />
<br />
原则上讲&nbsp;&nbsp;structured desk不算一个trading desk, 而是联系exotic traders, issuers, investors, rates quants的重要一环。所以虽然对于structurers来说&nbsp;&nbsp;一般学不到太多hedging techniques， 但是对于term structure of interest rates, credit market etc.还是会慢慢建立一些了解。特别是当housing crisis之后的credit crisis爆发之后&nbsp; &nbsp;对于credit dynamic的理解就变得至关重要。 Wall St.的major players, 5-yr CDS-level一度都在200-300bps之上&nbsp;&nbsp;这样很多corporate-issued bond都有不小的mark-down. 和credit相关的pricing issue一直是bond investors很关心的问题。<br />
<br />
Structured Products在primary 和 secondary market上面都很活跃&nbsp;&nbsp;而了解market appetite 是需要很多social skills的&nbsp;&nbsp;维持和high-profile individual investors &amp; institutional clients的关系&nbsp;&nbsp;是要经过多年的积累才能慢慢掌握的<br />
<br />
Lehman Bankruptcy 之后&nbsp;&nbsp;很多re-hedge activities发生&nbsp; &nbsp;对于in-the-money investor，就很急于找到新的counterparty继续他们的swap. 对于out-of-money investor, 就很想take advantage of Lehman Bankruptcy, 什么也不做而消除obligation. 因此supply-demand imbalance导致了严重的yield-curve 变动&nbsp; &nbsp;30-2 CMS spread也发生了逆转&nbsp; &nbsp;这些给pricing, hedging都带来了不小的挑战&nbsp; &nbsp;记得去年的11-12月&nbsp;&nbsp;很多时候是如坐针毡。<br />
<br />
不过这种好多个decades都不见的金融现象&nbsp;&nbsp;给practitioner 和学术界都提供了很多好的信息&nbsp; &nbsp;很多survivor的经验也变得尤为宝贵。<br />
<br />
3) Difference between Academia and Industry.<br />
<br />
这里的Industry主要指financial industry.<br />
<br />
整体来讲Academia的lifestyle比较flexible. 而在wall st.工作的不确定性比较大&nbsp;&nbsp;工作时间也比较固定<br />
<br />
当然从compensation上面说&nbsp; &nbsp;如果在trading desk坚持做几年&nbsp;&nbsp;比较优秀的话&nbsp; &nbsp;一年$500k+ (including bonus)还是有可能的&nbsp; &nbsp;当然现在新的Obama Admin.对bonus做了很多限制以后很多事情不容易预期&nbsp; &nbsp;但wall st.比其他领域收入高很多这个事实不会改变。<br />
<br />
如果是在academia, 因为是business school下面的faculty position, 起薪应该略低于$200k/year. 拿到tenure之后会有一些进步。当然很多hedge fund会请一些professor去做consulting, 也有些教授和MBA students一起开hedge funds. 但是如果过分热衷non-academic work，我想对学术能力多少会有负面的影响。&nbsp;&nbsp;Anyway, 看个人喜好。<br />
<br />
最近看到了academia领域里面很强很强的人&nbsp;&nbsp;真的很钦佩他们。从H. Hong, N. Barberis, 到W. Xiong, N. Wang 这些年轻的学者显示出他们很强的学术能力&nbsp; &nbsp;不少华人慢慢在finance领域做出的不错的成绩&nbsp; &nbsp;以后我想自己一定要多多努力&nbsp; &nbsp;学习他们身上的闪光点<br />
<br />
综合finance 和econ来说，如果是数理基础比较好的人&nbsp;&nbsp;做一些theoretical asset pricing, econometrics, 或者theoretical micro可能会有些优势&nbsp; &nbsp;对于很有economic sense的人&nbsp;&nbsp;做game theory, macro可能会好些&nbsp;&nbsp;很多时候 academic research都和一些empirical work相关联&nbsp;&nbsp;从form dataset, run regression 到robustness test, 解读分析结果&nbsp;&nbsp;看似straightforward的过程其实有很深的学问&nbsp; &nbsp;所以如果是想做empirical方面工作的&nbsp; &nbsp;应该也是越早接触越好吧.<br />
<br />
<br />
4)Job Searching<br />
<br />
在美的college students一般都用monster-track来投递简历&nbsp;&nbsp;但对于experienced employee就往往通过head-hunters. 和相关人士长时间保持联系很重要&nbsp; &nbsp;很多时候你会发现&nbsp;&nbsp;圈子很小&nbsp;&nbsp;大家其实都认识&nbsp; &nbsp;所以要努力保持好的reputation.<br />
<br />
个人觉得&nbsp; &nbsp;在New York/London的一些position，对于培养好的global view更有帮助。很多这边的金融产品在亚太区不做。中国的trading没有完善，而HK, Singapore有不少是liquidity issue.&nbsp;&nbsp;除此之外&nbsp;&nbsp;能够在global financial center&nbsp;&nbsp;有很多参加conference/conference call的机会。Conference每年都有好些, banker, trader, quant都有各自的&nbsp;&nbsp;而每天在工作中的conference call是很锻炼人的。在很短的时间内听London, New York, HK, Japan, Mexico (emerging market) etc.的trader, quant讲market view，慢慢的就可以培养一些market sense出来<br />
<br />
5) Career Decision Making<br />
<br />
很多人问过我到底该做什么样的工作&nbsp; &nbsp;其实兴趣和自己的强项 这始终是最重要的两个因素&nbsp; &nbsp; 如果盲目追求收入&nbsp; &nbsp;一定不会快乐&nbsp;&nbsp;也很难长久<br />
<br />
如果是比较喜欢安逸生活的人&nbsp;&nbsp;在加州的软件公司谋职可能比较合适&nbsp; &nbsp;对于喜欢变化和挑战的人&nbsp;&nbsp;wall st.的position会更适合一些&nbsp; &nbsp; 如果特别aggressive，business sense又很敏锐的人&nbsp;&nbsp;可能读一个top business school MBA 然后做private equity是不错的选择。选择适合自己的&nbsp; &nbsp;而不是过分和别人攀比&nbsp;&nbsp;同时不要局限华人圈子&nbsp;&nbsp;而是关注大的picture 相信一定会有所成的。<br />
