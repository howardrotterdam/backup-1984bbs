　图片说明：广州美女主播张小莉<br />
　　<br />
<br />
　　东方网6月14日消息：一个是广州商界精英，拥有千万身家，一个是著名美女主播，才气和美貌兼备。他们是一对曾经人人羡慕的爱侣，<br />
却为离婚分家产打了4年的官司，男方不愿离婚声称感情未破裂，女方则去意坚定要求分家产。在法院终审判决两人离婚并分割财产后，这起官司又因广东省检察院提出抗诉，于6月9日在广州市中级法院再次公开开庭审理。<br />
<br />
　　一次偶遇 造就精英与美女佳话<br />
<br />
　　家住广州市越秀区的余泽生，在广州商界摸爬滚打多年，在几家大型公司占有股份，于业界声望颇隆。1997年前，他在自己属下一个公司的活动中，结识了著名电视节目主持人张小莉，立即被这位女主播美貌和气质所打动。虽然余泽生刚从一段失败的婚姻中解脱出来，并有一个与前妻所生的儿子，但经历一段苦闷的单身生活后，见到张小莉后，余泽生重新产生了找个女人过日子的念头，随即对张小莉展开“感情攻势”。<br />
<br />
　　经过余泽生锲而不舍的努力，终于俘获了这位美女主播的芳心。1997年2月，两人结为夫妻，一时之间，商界精英与美女主播的故事传遍了圈内圈外。<br />
<br />
　　旧情不再 女方起诉离婚分家产<br />
<br />
　　但这段美好的姻缘只维系了4年，两人随后为了离婚析产官司又打了4年多。2001年11月，张小莉先是离家分居，接着在第二年一纸诉状送到原东山区法院，以双方性格不和，缺乏了解，感情破裂为由，提出要与余泽生离婚，并要求对夫妻财产进行分割。<br />
<br />
　　余泽生却不愿意两人就这样结束，还试图挽救这段婚姻。庭上，余泽生声称两人感情一直很好，他曾带着张小莉到日本旅游，并将公司的宝马车给她使用，并办了信用卡给她随意使用等等，可见他对张小莉的感情和信任。余泽生认为妻子提出离婚是受人指使，一旦醒悟过来是不会离婚的。<br />
<br />
　　但张小莉的去意已决，坚持要求离婚，并要求法院对她和余泽生在婚后买的房子、车子、以及余泽生所有的公司的股份收益，共价值1600多万的财产进行分割。<br />
<br />
　　诉讼之路 3年官司美女分得600万<br />
<br />
　　因案件涉及到对财产的查封和审计的复杂过程，原东山区法院在2002年受理此案后，2004年5月才有一审结果。法院判决两人离婚，并对财产进行了分割，在扣除了双方应该承担的共同债务，以及股份转让中的一些其他费用后，张小莉从中分得了360多万的财产和房屋。<br />
<br />
　　但对这个一审判决，两人均表示不服，分别向广州市中级法院提出上诉。余泽生认为，两人感情并未破裂，仍有挽回余地，法院不应该判决离婚，在分割财产方面的处理也有所不妥；而美女主播则认为她应该分得更多的财产。<br />
<br />
　　2005年8月份，广州市中级法院二审审理此案后，作出了终审判决，张小莉的上诉意见获得支持，总共分得近600万的夫妻共同财产，比一审的判决多分了近200万元，而余泽生的意见却不被采纳。此时，距离张小莉最初的起诉已经过了3年。<br />
<br />
　　波澜再起 检院为男方喊冤要重审<br />
<br />
　　眼见这场婚姻怎么也无法挽回了，余泽生死了心，开始把注意力重点转向了财产分割。他对广州市中级人民法院的判决不服，遂向广东省人民检察院提出申诉。广东省人民检察院经过仔细审查后，于2005年11月向广东省高级人民法院提起了抗诉。<br />
<br />
　　抗诉书认为，在此前的判决中，两人共同生活期间大量的生活支出并没有由双方分担，而是判定由余泽生独自承担，对他明显不公；此外，把没有经过清算的公司获益直接分了一半给妻子一方，亦属不妥，因为其中还有其它公司应占的部分没有剔除，而且公司还有一些债务没有偿还。基于以上原因，抗诉书请求改判，要求妻子张小莉也承担相应生活支出，并退回多分的公司收益。<br />
<br />
　　因为检察院的抗诉，今年6月9日，广州市中级法院另行组成合议庭对该案涉及的财产部分进行再审，这对“冤家”第三次在法庭内碰面。<br />
<br />
　　这起包含了金钱美貌、爱恨情仇和权利义务等多种复杂因素的官司，最终将以什么样的结局收场？<br />
<br />
　　 相关知识：检察院抗诉分刑事民事两大类<br />
<br />
　　据了解，“抗诉”是作为法律监督机关的检察院的一项基本职能，专门针对法院的判决而言，依据相关法律规定，检察院抗诉的案件不管最终是否合理合法，法院都必须要开庭审理。<br />
<br />
　　抗诉分为两种，一种是刑事抗诉，分为两种情况，分别为在法院对刑事案件作出一审判决还没生效时；或者在刑事判决生效后依审判监督程序提起。比如，众所周知的广州两大涉黑案――周广龙案和简竹醒案，都是在广州中级法院的一审判决还没生效时，由广州市检察院向广东省高级法院提起抗诉。<br />
<br />
　　一种是民事抗诉，是由检察院内部的控告申述部门受理当事人的反映后，认为法院的民事判决确有不公之处的，向法院提出抗诉，此种抗诉的一个特点是判决必须是已经生效，上述的案件就是一个典型。据悉，此种情形并不常见，原因在于民事案件不像刑事案件那样直接由检察机关经办，大多数情况要根据当事人的投诉来决定。<br />
