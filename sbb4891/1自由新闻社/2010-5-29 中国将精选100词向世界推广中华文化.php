													<h2>中国将精选100词向世界推广中华文化</h2>
						
						
																											<div id="postmessage_645838" class="t_msgfont">中新社北京5月28日电 (记者应妮)在2010年俄罗斯“汉语年”期间，中国将精挑“中国、京剧、烤鸭、书法、孔子”等100个代表中国传统文化精髓的词汇，以广播、电视、网络、手机等多媒体形式，向俄罗斯民众介绍；此后还将打造英语、法语、西班牙语、日语和阿拉伯语版。<br />
<br />
　　根据中俄两国政府的决定，2010年是俄罗斯的“汉语年”。作为俄罗斯“汉语年”期间重要活动之一的大型多媒体文化项目《你好，中国》28日在北京正式启动。俄罗斯驻华大使拉佐夫、国家广电总局副局长田进等嘉宾一起启动了该项目的官方网站。<br />
<br />
　　《你好，中国》将选取100个代表中国传统文化精髓的汉语词汇，如中国、京剧、烤鸭、书法、太极拳、春节、风筝、熊猫、矛盾、吃醋、知音等，从不同侧面反映中国文化的博大精深，加深国外民众对中国和中华文化的了解；同时，围绕这100个词汇将制作出几种不同媒体形态的产品，即：100集电视系列片、100集广播教学节目、由100节课文组成的纸质教材，以及集纳了以上三种产品的《你好，中国》官方网站。<br />
<br />
　　除此之外，《你好，中国》还将推出一个网络汉语大赛。俄罗斯网友可以网站的在线录音功能，在线展示他们学习汉语的成果。而网络汉语大赛的优胜者，将获得来中国参观、访问的机会。<br />
　　主办方表示，《你好，中国》将被打造成一个面向全球各个国家民众的多语种文化项目。首批计划开发英语、法语、西班牙语、日语和阿拉伯语版。<br />
<br />
　　《你好，中国》由国家广电总局主办、中国国际广播电台策划实施。<br />
