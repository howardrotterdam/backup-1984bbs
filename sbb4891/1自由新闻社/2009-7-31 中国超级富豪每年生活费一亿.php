<br />
　　均热衷送子女海外深造 <br />
<br />
　　北京贵族的消费主要由住宅和室内装饰、车、会所、个人理财等组成。北京富豪热爱艺术，一年的钢琴课消费是5万元；家庭新增年消费为64.55万元，主要用于购买古董、字画等。 <br />
<br />
　　上海新贵族一年来家庭新增年消费为339.638万元，主要用于今年新车购置、慈善捐款和收藏。 <br />
