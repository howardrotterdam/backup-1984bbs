　　香港《文汇报》报道，报告称消费门坎是富豪们追求精致生活方式所需要的成本（不包括个人任何其它投资），根据富豪们当前固定资产总值和消费总额来确定。调查发现，中国新贵们的消费，一半给了房屋和室内装饰。以上海为例，家庭消费中包括两套自住房产，一幢上海郊区别墅和一套市区高级公寓。上海新贵族在豪宅内不但收藏?瓷器、玉器等古董，还有著名画家的作品。在客厅摆放施坦威钢琴，悬挂意大利Baccarat水晶灯等。胡润指近5年，高档房地产将是影响中国新贵族生活方式最大的一个因素。 <br />
<br />
