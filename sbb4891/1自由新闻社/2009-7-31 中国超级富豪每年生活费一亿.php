<br />
　　深圳富豪的家庭年新增消费为194万多元，主要用于购买意大利进口家具、高档音响和银行理财产品。 <br />
<br />
　　成都富豪拥有麓山国际别墅、价值80万元的定制全套家具、奥迪、宝马、私人理财产品等。其家庭新增年消费为78.018万元。 <br />
