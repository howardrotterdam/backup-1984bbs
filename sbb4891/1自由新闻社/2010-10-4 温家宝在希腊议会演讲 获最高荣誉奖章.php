													<h2>温家宝在希腊议会演讲 获最高荣誉奖章</h2>
						
						
																											<div id="postmessage_854119" class="t_msgfont"><a href="http://www.taihainet.com" target="_blank">www.taihainet.com</a>　2010/10/4 7:00:16　中新网　【字体：缩小 放大】<br />
 <br />
<br />
&nbsp; &nbsp; 中新社记者 周兆军<br />
<br />
&nbsp; &nbsp; 正在希腊访问的中国总理温家宝3日来到希腊议会大厦，面向希腊议员和多国驻希腊使节发表了题为《坚定信心，共克时艰》的演讲。演讲结束后，希腊议长佩察尔尼科斯向温家宝授予希腊议会最高荣誉奖章――“伯利克里斯”金质勋章，表彰他对发展中希关系所做的贡献。<br />
<br />
&nbsp; &nbsp; 当天早上，温家宝参观了希腊著名古迹雅典卫城。在演讲的开场白中，温家宝说：“今天早晨，帕潘德里欧总理陪我登上雅典卫城。看到这座有2500年历史的古城，我感慨万千。我看到了一个充满阳光、充满文明、充满欢乐、充满和谐的希腊。”<br />
<br />
&nbsp; &nbsp; 这是温家宝第一次访问希腊。他说，希腊是一个令人向往的地方，访问这个古老而美丽的国家，是他多年的夙愿。<br />
<br />
&nbsp; &nbsp; “小时候，我就读过许多古希腊的故事。从那时起，我就对古希腊文明产生了浓厚兴趣，她的久远与辉煌一直在深深地吸引着我，令人神往。”温家宝说：“古希腊崇尚智慧、崇尚理性、崇尚科学的精神，始终是我秉持的重要人生信条。亚里士多德那句‘吾爱吾师，吾更爱真理’的名言，至今仍激励着我。”<br />
<br />
&nbsp; &nbsp; 当前，希腊正面临主权债务危机的严重冲击，经济发展面临许多挑战。温家宝此次来访，带来许多利好消息，表达中国对希腊的坚定支持。<br />
<br />
&nbsp; &nbsp; 温家宝引用古希腊哲学家苏格拉底的话说，“逆境是磨练人的高等学府。”中国也有句古语，“艰难困苦，玉汝于成”。各种坎坷和灾难，没有能够阻止我们前进的步伐，而是铸就了两个民族百折不挠的坚定信念和品格。<br />
<br />
&nbsp; &nbsp; 他表示，中希两国友好交往源远流长。早在公元前四世纪，中国丝绸就已经输往希腊，希腊人民曾给中国起了一个美丽的名字――“丝之国”。在中国，发源于古希腊的奥林匹克运动家喻户晓，丰富多彩的希腊神话广为流传。中华人民共和国成立后，希腊船东率先冲破封锁，给中国人民运送了急需的物资和设备。中国人民将永远铭记这一切。<br />
<br />
&nbsp; &nbsp; “今天早上，我想起数学家陈省身教授。他在临死的时候，用颤抖的手在纸上写下了两个字：希腊。他对亲人们讲，我要回到数学的发源地希腊去了。这件事情一直令我感动。”温家宝说。<br />
<br />
&nbsp; &nbsp; 他表示，作为希腊的朋友，中国政府坚定不移地支持希腊，希望通过加强合作为希腊早日克服困难出一份力。<br />
<br />
&nbsp; &nbsp; 在演讲中，温家宝还全面论述了中国与欧盟的关系。他指出，中欧双方处于不同的发展阶段，经济互补性很强，没有根本的利害冲突，都奉行多边主义，主张维护世界文明的多样性，都是国际体系的积极参与者和建设者。中欧关系的稳定发展，为双方带来了实实在在的利益。<br />
<br />
&nbsp; &nbsp; 温家宝表示，一位欧洲思想家曾说，“没有古希腊文化及罗马帝国所奠定的基础，也就没有现代的欧洲。”我们衷心希望，希腊在推动中国与欧洲关系发展方面发挥更加积极的作用。<br />
<br />
&nbsp; &nbsp; “希腊有一句名言：‘技能加上信心，是无敌的军队。’人类社会正是在不断战胜各种灾难和挑战中走向文明和进步的。”温家宝说，丰厚的文化积淀，培养了我们这些古老民族智慧勇敢的英雄气概，这是信心和力量的源泉。“我相信，只要我们认真继承和弘扬先人留给我们的优秀成果，充分依靠和发挥人民的聪明才智，坚定信心，共克时艰，就一定能够创造更加美好的明天。”</div>

							
							
							
							
