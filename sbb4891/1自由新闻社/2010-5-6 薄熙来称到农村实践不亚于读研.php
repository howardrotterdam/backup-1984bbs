													<h2>薄熙来称到农村实践不亚于读研</h2>
						
						
																											<div id="postmessage_600229" class="t_msgfont">　　星岛环球网消息：在“五四”青年节之际，市委书记薄熙来、市长黄奇帆与大学生“村官”代表座谈，勉励他们坚定理想信念，融入农村，服务农民，在农村广阔天地锻炼成长，为重庆发展贡献智慧和力量。市领导张轩、何事忠、马正其、范照兵、陈存根、余远牧、陈景秋参加了座谈。<br />
<br />
　　《重庆晚报》报道，座谈会开始前，大学生“村官”们齐唱《在希望的田野上》、《光荣啊，中国共青团》，歌声嘹亮，充满青春的活力。<br />
<br />
　　在市委组织部和有关区县、乡村的领导汇报大学生“村官”的工作情况后，大学生“村官”们便接过话筒，踊跃发言。<br />
<br />
　　“村官”虽不好当，但只要有信心、肯奉献，对群众有真情，做群众工作讲方法，大学生就能够有所作为<br />
<br />
　　“从象牙塔到大农村，对我来说，是一个挑战和跨越。”彭水自治县汉葭镇镇南村党总支书记周晓琳说，一年多里，她走遍了全村715户，工作虽然繁琐而单调，但她明白，只有把这些小事做好了，才能担当更大的责任。<br />
<br />
　　荣昌县远觉镇狮子桥村主任助理李坤初次走访村民，看到端来的长板凳上有油渍，拿出餐巾纸来擦，搞得很尴尬。现在，他逐渐认识到，对待群众要有真情，做群众工作要讲方法，让群众做的事自己先带头，农民才会把你当“自家人”。现在，他带头创办养牛场，已经引导10户农民加入。<br />
<br />
　　黔江区冯家街道渔滩社区党总支副书记邹专圆说，渔滩社区修电站淹没了农民土地，矛盾较大。刚到社区工作时，听说他在“政府”工作，农民连橘子都不卖给他。后来，他们帮助农民办实事，村民送橘子给他吃，还托他向薄书记表示感谢。说着，他站起身，向薄书记鞠了一个躬。<br />
<br />
　　城口县明通镇白台村主任助理朱子玲说，自己从小在主城区长大，到了农村才发现，贫困村“村官”不好当，但只要有信心、肯奉献，大学生就能有所作为。<br />
<br />
　　秀山县钟灵乡石门村党支部副书记黄槐则就“唱读讲传”活动建议说，利用农村的大喇叭，播放红歌，朗读经典，利用村级远程教育中心放一些老电影，群众很欢迎。<br />
<br />
　　在广阔天地里学习、实践，不亚于读个研究生<br />
<br />
　　听完大家的发言，薄熙来说，同学们的体会很真实，很鲜活，充满了青春朝气，大家在农村做出了不少成绩，真为你们高兴。老百姓是真正的唯物主义者，你只要全心全意地工作，就能得到农民兄弟的认可。<br />
<br />
　　薄熙来说，大学生“村官”工程，是培养社会主义事业接班人的重要战略。我们的教育环境，不仅要有大中小学和研究生院，还有农村这个广阔天地，这是培养人才的好地方。每年2000多名“村官”到农村接受锻炼，在广阔天地里学习、实践，很多东西是在校园里得不到的。同学们学到、感悟到的东西，不亚于读个研究生。毛主席曾经说过，“读书是学习，使用也是学习，而且是更重要的学习”。同学们要使学到的知识更好地消化吸收，成为心领神会、入木三分、运用自如的东西，就必须到实践中体验、体会，进而提炼、升华。因此，当好“村官”，既是为社会做贡献，也是把学习和社会实践相衔接，实现思想飞跃的重要阶梯，大家一定要格外珍惜这样的机遇。<br />
<br />
　　愿以“天下为己任”，将来报效祖国的同学们，就需要了解国情，了解农民<br />
<br />
　　薄熙来说，有人认为农村落后，嫌农民“土”。怎么看待农村？大家需要静下心来好好想一想。其实，对中国人来说，广大的农村有最基本的知识，几亿农民中有最本源的感情，改善他们的生产生活状况是最重要的任务，能与他们自然亲切地交流才算学会说话。<br />
<br />
　　薄熙来说，我国是个农业大国，人口中农民占了多一半，讨论任何重大问题，都离不开农村，不论是经济问题还是社会问题，讲到底，都必须解决农民问题。了解中国的国情，也须首先了解农村，了解农民，这是最基本的知识。近现代中国革命和建设的主要问题，集中在农民和土地问题；改革开放也是从解决农村和农民问题开始的。只有把农村问题吃透了，才能准确地把握国情。愿以“天下为己任”，将来报效祖国的同学们，首先需要了解国情，了解农民！<br />
<br />
　　薄熙来说，我们经常讲，要爱人民，对人民有感情。而对人民的感情是具体的，不是抽象的。我国有七、八亿农民，对人民群众的感情，在很大程度上就是看我们对农民的感情，对他们有没有深刻的认知和亲切的关爱。我们的党是植根于广大农村的，而千千万万的农民源源不断地充实到我们的队伍中来，才使中国革命取得了胜利。同学们到农村，就是体会并增进这种最本源的感情。<br />
<br />
　　讲话，写文章，作报告，一定要让人听得懂，讲得自然，不要打官腔<br />
<br />
　　薄熙来说，在农村，还可以学到人民群众喜闻乐见的语言。讲话，写文章，作报告，一定要让人听得懂，讲得自然，不要打官腔。现在有些干部，会开得多了，习惯说些官话、套话，虽然讲得很认真，但老百姓听不懂，双方的距离就远了。你看春节晚会上，赵本山的节目为什么受欢迎，很简单，就是他用老百姓的话，亲切、朴实，富有生活气息，还有新鲜感，就中听。同学们在农村干上几年，语言风格和习惯也会受影响，你讲的话老百姓能接受，才有开展工作的条件。<br />
<br />
　　薄熙来说，作为有志报效祖国的一代新人，有几年农村工作的经历，了解了农民的生产、生活和需求，得到基层的知识、朴素的情感和群众的语言，今后几十年无论做什么工作，感觉都会大不一样，就容易和群众打成一片。<br />
<br />
　　到农村就要“放下臭架子，甘当小学生”，虚心向百姓学习，不断提高自己<br />
<br />
　　薄熙来说，大学生到农村工作，要清楚地认识自己的优势和劣势。大家接受了系统的教育，有知识、有文化，接触面广，眼界比较宽，思想活跃，又有创意，农民都把我们当宝贝。但也要看到自身的不足：书本知识比较多，实践经验却比较少；在情感、爱好上与人民群众有距离；城里住惯了，也还比较娇气；对农村、农民知之甚少，这方面的眼界又比较窄。对自己一定要一分为二，到农村就要像前辈讲的那样，&quot;放下臭架子，甘当小学生&quot;，虚心向百姓学习，不断提高自己。<br />
<br />
　　别看村子小，但“麻雀虽小，五脏俱全”，完全可以干一番事业<br />
<br />
　　薄熙来说，到农村广阔天地去工作，对大学生“村官”会有几个直接的收益：一是检验自己。大家在校学习十来年，到底学得怎么样？走向社会后行不行？每个人都要经过社会实践的检验。二是锻炼自己。同学们人生之路还长，要吃些苦，磨练意志力，而“意志者，人生事业之先驱也。”三是充实自己。农村一些地方虽然“一穷二白”，但蕴藏着丰富的知识，可以学到很多城里没有的知识，还可终生受益。四是净化自己。可以培养起对人民群众纯朴的感情。伟大的情感能产生伟大的力量，年轻时培养起对人民群众的真情实感，今后就能踏踏实实做些对国家有益的事。<br />
<br />
　　薄熙来寄语大学生“村官”，继续努力，为千家万户解决实际问题，多办“积德”的事，无论大事小情，积累起来就是成绩，也是经验。孙中山有句名言：“能为千万人服务时，就为千万人服务；能为千百人服务时，就为千百人服务；能为一、二人服务时，就为一、二人服务”。别看村子小，但“麻雀虽小，五脏俱全”，完全可以干一番事业。在发展经济的同时，还要开展好“唱读讲传”活动，继承和传播红色传统，还可以组织放电影，搞点体育运动，丰富农村文化体育生活。<br />
<br />
　　十年、二十年后再来看，最长进、最有出息的，可能是大学生“村官”<br />
<br />
　　黄奇帆说，重庆的年轻人生活在一个“幸福时代”，不是说物质享受丰富，而是现在的重庆，有大量的事可做，年轻人大有可为。重庆正在推动工业化、城市化和城乡统筹一体化，其中，城乡统筹一体化最为困难，市委、市政府操心也最多。重庆是全国统筹城乡综合配套改革试验区，担负着为城乡统筹探路的使命，大学生“村官”的工作是推动统筹城乡发展的一部分。大家为农民办理医保、社保等，是在为农民服务；带着农民修路、修桥、建设水电设施、沼气池，是在改善农村的基础设施；想办法解决农村的教育、文化、卫生、体育等问题，是在缩小城乡公共服务差距。推进城乡统筹，需要以改革的精神在体制机制上创新，站在农民的角度从细节上着力，“村官”是农村最基层的领导者，这些工作需要大家具体推动落实。<br />
<br />
　　黄奇帆说，大学毕业后，大家选择从最艰苦、最基层的农村起步，与留在大城市的同学相比，似乎吃亏了。其实，人生永远是平衡的，十年、二十年后再来看，最长进、最有出息的，可能就是从基层做起的大学生“村官”们。他对大学生“村官”提出四点希望：一是培养坚定的信念、意志和勇气；二是提高和农民打成一片的能力，和农民成为能聊天的知心朋友；三是提高发现问题、解决问题的能力，能为百姓解决问题，才能树立群众威信；四是加强学习，主动地学习中央、市委市政府的政策，学习各种社会常识。市政府也将建立专门的培训制度，定期组织大学生“村官”学习和交流，并提供解决实际问题必要的资源配置，为大学生“村官”开展工作创造有利条件。<br />
<br />
　　座谈会后，市领导与大学生“村官”合影留念，“村官”们再次合唱《光荣啊，中国共青团》。薄熙来说，当年修红旗渠，大家吃个红薯面的馒头就不错了，战天斗地，一往无前。现在条件好了，但全心全意为人民服务的精神不能丢，要努力改变渝东南、渝东北的农村面貌，这样，等你们六、七十岁，对儿孙回忆往事的时候该是多么自豪啊！<br />
<br />
　　书记、市长的一席话，引来“村官”们阵阵鼓掌欢笑。<br />
