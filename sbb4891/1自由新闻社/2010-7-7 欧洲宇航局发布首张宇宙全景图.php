																											<div id="postmessage_725447" class="t_msgfont">近日，欧洲宇航局公布普朗克望远镜拍摄的首张宇宙全景图。<br />
<br />
<img src="http://img2.cache.netease.com/cnews/2010/7/6/2010070616554303c8f.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
普朗克望远镜拍摄的首张整个宇宙的全景图像，普朗克望远镜去年被送入太空，以考察宇宙中最古老的光。<br />
<br />
网易探索7月6日报道 据英国《每日电讯报》报道，近日，欧洲宇航局公布普朗克望远镜拍摄的首张宇宙全景图。<br />
<br />
搭载普朗克望远镜的卫星耗资6亿欧元，去年由欧洲宇航局发射升空。普朗克望远镜被发射到距离地表近100万英里的太空，以记录宇宙的起源之光。普朗克天文台的任务是通过研究宇宙大爆炸（the Big Bang）之后遗留的热量，去考察宇宙的年龄、容量与进化。<br />
<br />
从去年九月份起，普朗克望远镜就开始发布它拍摄的首张宇宙图像，图像显示了跨越宇宙的狭长的古老光带。现在普朗克望远镜终于发布了整个宇宙的全景图像。这幅图像显示，在地球的另一端人类的仪器可观察到的物质，对波长非常长的光也具有敏感性。<br />
<br />
人类所处的银河系的大部分占据了这幅图像的主要部分。横贯图像中央的明亮水平线是银河系的主要光盘，我们太阳系的太阳与地球也身处其中。在银河盘面的上方与下方，还能看到冰冷的星尘以高达几千光年的速度快速向外迸发。科学家将要花好几年去分析这幅图像，以便更好地理解宇宙如何变成现在它所呈现的的样子。<br />
<br />
<br />
“你看到的是我们处于气体与星尘之中的银河系的结构，关于太阳的邻近星系是怎么回事，这告诉了我们数量惊人的信息；同时与其他星系的形成方式相比，我们银河系的形成方式是什么，这幅图像也告诉了我们大量的信息，”安德鲁・贾菲（Andrew Jaffe）教授告诉英国广播公司（BBC）新闻记者。他是一位来自伦敦帝国学院（Imperial College London）的普朗克望远镜研究团队的成员。</div>

							
							
							
							
