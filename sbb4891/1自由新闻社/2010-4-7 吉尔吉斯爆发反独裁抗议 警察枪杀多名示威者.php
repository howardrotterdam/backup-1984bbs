<br />
2009年2月3日，库尔曼别克・巴基耶夫总统宣布立即关闭玛纳斯空军基地――美国留在中亚的唯一军事基地。[3] 2月19日，议会以压倒性多数（78-1）通过了政府提交的关于废除美军租用玛纳斯空军基地协议基地的议案。[4] 然而，经过吉尔吉斯、俄罗斯、美国外交官的多次幕后协商之后，吉尔吉斯斯坦收回了关闭基地的决定。6月25日，吉尔吉斯斯坦议会批准了在美玛纳斯空军基地基础上建立转运中心的协议。 新的合同允许美国人继续使用该基地，然而租费从每年1740万美元涨到6000万美元。该基地将不再叫做“玛纳斯空军基地”，而更名为“物资转运中心”，只能转运非军事物资。玛纳斯空军基地的性质由之前的美军功能全面的军事基地，转变为只能向阿富汗运输非致命性设备的运输中转中心。[5][6][7]<br />
<br />
吉尔吉斯斯坦世界上二十个贪腐程度最高的国家之一：2008年吉尔吉斯斯坦的贪污感知指数是1.8（0为最腐化，10为最廉洁）。[8] 2010年4月７日吉尔吉斯当局，扣押反对派领袖，触发全国骚乱，局势失控，军警向示威者发射实弹，医生说，有数十人死亡，百多人受伤，全部死伤者是中弹；在首都比什凯克，数千名反政府示威者再度围攻总统府，士兵向人群开枪，数小时前，示威者抢夺装甲车撞向总统府大闸，防暴警员用实弹镇压；总统巴基耶夫及总理乌谢诺夫，颁布在全国实施紧急状态令，乌谢诺夫强调，政府仍掌控国家局势，绝不会同暴徒谈判，但当局其后释放十多名被扣押的反对派领导，其中一名反对派领袖，前总检察长别克纳扎罗夫声言，最终目标是夺取国家政权。在当地的新华社记者说，大批反对派支持者攻占国会大楼，总检察长被打至重伤，另一批示威者正试图攻占内务部和国防部，与警员交火，有政府建筑物着火，冒出浓烟；内务部长孔甘季耶夫被示威者扣为人质，其间一度传出死讯，第一副总理扎帕罗夫亦被胁持，示威者又占据国营电视台，市面出现抢掠，比什凯克国际机场暂时关闭</div>

							
							
							
							
