<br />
2002年三月发生在南部阿赫昔区六名抗议任意拘捕反对党政治人物的民众遭警察射杀的事件，引发了全国性抗议。在民间社团和反对党代表的参与之下，阿卡耶夫总统启动了宪法改革进程。此进程最初有政府、民间、和社会代表的广大参与和开放对话，产生了2003年二月投票过程有缺失的公民投票。公民投票通过的宪法修正案导致了总统扩权和国会与宪法法庭弱化。根据新宪法，在2005年的国会选举之后，原先的两院制国会将成为75席的单院国会。<br />
<br />
过渡政府领导人正在发展新的治理结构并着手解决重要的宪法争议。国家团结委员会宣布，六月将举行总统选举，而在其三个月之后将举行国会选举。<br />
