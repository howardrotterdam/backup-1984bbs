Crisis in Kyrgyzstan<br />
Widespread anti-government protests in Kyrgyzstan recently turned violent, with groups of opposition protesters attempting to storm some government buildings, and clashing with riot police. Tensions are high, as Kyrgyz authorities declared a national state of emergency, and are enforcing curfews in at least three cities. As riot police fired on opposition members and protesters fought with stones and captured weapons, estimates of casualties have varied widely, with reports ranging from 12 killed to over 100 killed. Opposition leaders appear to have taken over some of the national television channels, and President Kurmanbek Bakiyev appears to have fled the capital, Bishkek overnight. As the situation in Kyrgyzstan remains in flux, I may post more photos here later, as warranted. (38 photos total)<br />
