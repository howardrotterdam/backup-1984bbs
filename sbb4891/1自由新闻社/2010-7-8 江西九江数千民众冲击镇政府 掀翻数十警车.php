<br />
7月5日下午五点开始，江西省九江市修水县港口镇发生大规模群体事件。因截访回来的女村民被当地公安殴打昏迷不醒，数千愤怒围观群众将镇政府围的水泄不通，用石头、砖块砸毁镇政府办公室所有玻璃窗，数十辆警车被民众推翻、砸烂玻璃，当时马路上和镇政府一片狼藉，那些官员早已吓得逃跑了。<br />
数千人民众砸镇政府 推翻数十辆警车<br />
<br />
洞下村九组村民于先生接受大纪元采访时说:“当天晚上，那些警察打村民，旁边围观的群众不少于一万人，整个港口镇老百姓全部看到了，激起了很大的民愤。镇政府的大门岗刚好建房子，大门口有砖块，用那个砖块砸了政府的玻璃，没有去砸派出所，加上湖北的有三十多辆警车，警察有三百多人，老百姓愤怒了，把车子掀翻，砸掉了十八辆警车。”<br />
<br />
“老百姓冒着生命跟政府拚死，你说这个政府工作有没有做到位，哪有这样的政府，现在社会真是黑暗的不得了，共产党的天下这么黑，老百姓活不下去了。”<br />
<br />
当天被打的两位女村民，现在仍昏迷不醒，在医院抢救。一个姓易的在南昌附属医院抢救，一个姓程的在清水县人民医院。“现在第四天了，都还没有清醒过来，做那个条件反射没有一点反应。还有三个男民众，二个重伤，一个轻伤。”于先生说。<br />
<br />
<br />
两省公安围追堵截上访村民<br />
<br />
5日，港口镇洞下村村民因不满当地政府强行搬迁而集体到北京上访，当地政府为了阻止村民上访，派出警车及100多公安威胁客车司机，不许村民坐车，一百多老老少少村民不得不徒步50公里绕道湖北去北京，最后当地公安出动30辆警车将村民拦截回镇政府。<br />
<br />
于先生说:“5日，二个组大约有一百多人准备到北京上访，县政府派了一百多警察来抓，不让我们走，当时县委书记和安全局的人打电话给湖北省领导，到港口镇交界的地方，那边出动了一百多名警察来抓人。”<br />
<br />
“因为不让坐车，这二组的老百姓就步行50公里，进入了湖北今塘镇(音)交界的地方，公安还交待当地的老百姓不给我们吃饭，不让我们住宿，不让我们坐车。”<br />
<br />
最后老百姓没办法就停下来了，当时修水县书记承诺：只要村民不要上访，回去把这个事情谈好。<br />
<br />
回镇政府后，那些领导就不跟村民讲条件了，当时县长、书记、公安局局长就开车提前跑掉，八、九组的老百姓就在那里等，有位县长的车子被村民拦下，要求谈判。<br />
<br />
于先生向大纪元记者详细描述了现场的情况。<br />
<br />
于先生说:“他就叫警察打她们，当时的场景惨不忍睹。那个女的跪在那里叫青天，叫官把这个事情讲好，那些老百姓不愿意起来，警察把那个女的用脚、拳，拉过来就是打。打伤二个女的，其他人躺在地上拦在路上。”<br />
<br />
“这些警察抓着那些女人的头发一个一个往旁边拉，那些村民就不让县长的车子过，警察抓着头发，拖着脚，就像打狗一样，警察就是使劲打，打了好多人，重轻伤的有二十多个人。警察还用脚踩在村民胸口，用手掐女人的脖子，一直到现在都昏迷，还没有说话，都不清醒。”<br />
<br />
“被打的那些女人的老公过去把她掀过来，警察就把她老公的手扳过来，掐着脖子，摁在地上，五个警察打一个村民。当时抓了大概有二十多个人，都给港口老百姓给抢出来了。现在县里还调了好多警察来镇压老百姓，不算我们八、九组的村民，今天上午听说抓了三十多个。我也不敢出去，现在手机都被监控了。”<br />
<br />
记者致电港口镇镇政府了解此事，一位男工作人员表示，现在正在处理这个事，网上的报导绝大多数是事实(官方报导)，受伤村民情况好的很，有一个在医院里面，这个不是打的，她本身自己有脑膜炎，她自己本身有病。<br />
<br />
这位工作人员否认公安有殴打百姓和抓人?他说：“现在有一部份人在造谣吗?比如说警察打老百姓，这些都是造谣。事实是有3个早期犯了刑事案件的人，公安在抓捕时，正好碰到老百姓要上访的情节，他们就把这事扯在一起，现在就跟老百姓说明事情的真相。”<br />
<br />
当天在现场参与做思想工作的乡镇干部卢先生告诉大记元记者，“在湖北被镇干部和公安拦回来了，回到港口把路堵了，造成交通堵塞，公安拉他们走，就造成这个事件。当时抓了三个村民，到了晚上九点，港口镇的围观百姓越来越多，民愤很大，就把镇政府的玻璃和公安警车全部砸了。”<br />
<br />
钨矿企业污染严重 村民被逼搬迁<br />
<br />
近年来，因钨矿市场价格的暴涨，位于修水县境内的江西省修水香炉山钨业有限公司钨矿的开采规模也越来越大。在上世纪80年代中期起，在这里陆续开办了大大小小的数十家钨矿企业。<br />
<br />
洞下村位于海拔700多米高的香炉山中，山清水秀。因钨矿排放的大量污水造成矿山脚下溪水严重污染，已影响洞下村八、九组村民生存环境。<br />
<br />
山西矿尾渣溃坝事件发生后，江西安检部门发现香炉山钨矿堆放的矿尾渣存在严重安全隐患，经检测，所排泄的污水PH值严重超标。<br />
<br />
村里的小溪水遭到污染后，气味刺鼻难闻。不少村民的皮肤出现溃烂情形，各种牲畜吃了被污染的水、草后都相继死亡，严重影响当地村民的日常生活。<br />
<br />
卢先生表示，那个污染影响很大，牲畜养不活了，河里鱼虾都没有了，水不能喝。老百姓担心尾矿沙被水冲下来，把他们埋掉。<br />
<br />
因此，村民多年上访投诉。当地政府做出移民方案，将村民安置在港口镇或修水县城。<br />
<br />
村民此次集体到北京上访有两个原因，一、这钨矿经检查是危险坝，坝下面二个村庄一共有300人左右，不搬家生命就遭到危险。县镇领导只给一个人补贴2,500元，村民认为补偿太低，要求稍微加一点，但一直没有达成一致的协议。<br />
<br />
二、因为搬迁要村民签字问题，当地县政府把九组组长的儿子抓过去，威胁九组组长要他同意搬迁。村民很气愤：政府哪有这样的行为，把人家的儿子抓去做人质，作为交换条件，逼村民搬迁的条件，怎么能这样呢？<br />
<br />
据村民投诉，香炉山钨矿把移民的钱拨下来了，但被县里贪污了，村民为此去闹了很久也没用，最后村民去北京告状。<br />
<br />
阻止施工 村民遭催泪弹驱赶<br />
<br />
