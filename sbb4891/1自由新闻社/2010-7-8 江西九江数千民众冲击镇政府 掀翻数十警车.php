修水县委宣传部相关文字材料称，事件缘起于香炉山钨矿尾矿库下游的港口镇洞下村村民对移民搬迁补助不满所致。经证实，香炉山大钨矿企业自2003年被中国五矿集团整体收购后，产业规模不断扩大，选矿所排放的尾渣也越来越多，尾矿库的尾渣堆放已经超过百米之高，对附近村民的生命财产安全形成威胁。<br />
<br />
江西省安监局和九江市安监局多次下达通知要求该矿进行整改，修水县政府也对尾矿库进行了安全治理，并于今年3月份出台移民搬迁方案，拟出资2000多万元人民币(平均每户30余万元)帮助洞下村两个组66户群众移民搬迁到县城。<br />
<br />
修水县官方表示，部分群众提出按照每人20万元(相当于每户约100万元)的搬迁补助，并按九江城区安置价评估补偿。在条件未得到满足的情况下，部分群众多次到北京、省、市进行上访。7月5日凌晨5时许，上访群众租用12辆昌河车从洞下村出发，途中受到政府工作人员劝返，但均未见效。<br />
<br />
<br />
14时许，上访群众行至湖北省崇阳县金塘镇，经反复做工作后，同意返回并商定派代表与政府商谈。在返回至港口集镇丁字路口时，部分群众开始聚众围堵过往车辆，数名妇女躺在路中央，少数群众还向民警和工作人员掷石块，现场一度失控。在有关人员的劝说下，上访群众开始疏散。<br />
<br />
当晚9时许，群众再度聚集并冲击港口镇政府和森林派出所，用石头、砖块打砸办公楼门窗、灯具、用具等，打伤多名公安干警和镇村干部，砸坏数辆警务车。<br />
<br />
当地政府主要领导立即赶赴现场平息事端，并对重点区域部位进行重点治安维护，以防事态进一步扩大。目前，局势已得到有效控制，各项善后工作正在有序展开，具体情况正在进一步调查中。<br />
