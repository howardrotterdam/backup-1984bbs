																											<div id="postmessage_539132" class="t_msgfont">昨晚的政府新闻发布会最新的说明，楼主的那个名单基本被征服推翻了，这个事情爱卡川分闹得很厉害，从一开始就是川分在闹，过程十分有意思！！下面是政府的通告：<br />
成都市食安办通报“大丰香油”事件称，是香精兑玉米油冒充“芝麻香油”<br />
<br />
昨晚8点30分，成都市食品安全委员会办公室（下简称：市食安办）举行新闻发布会，邀请工商、公安、卫生、质监等行政监管部门，就谷丰香油坊非法加工销售“大丰香油”事件的最新情况向社会通报。<br />
<br />
 <br />
 <br />
 <br />
・巴望海徒步FB游 ・票选四川最美“足球宝贝” <br />
・援助意外怀孕少女 ・对付宫颈疾病全攻略&nbsp;&nbsp;<br />
 <br />
<br />
经查，该香油坊非法加工的芝麻香油是用食用玉米油和食用香精勾兑而成，属于非法加工、“以假充真”的芝麻香油。证据表明，可以排除该香油坊生产销售“潲水油”的嫌疑。<br />
<br />
目前，谷丰香油坊的3名涉案人员已被刑拘。现场还公布了违规使用“大丰香油”的13家火锅店名单。<br />
<br />
案件进展3涉案人员已被刑拘<br />
<br />
成都市食安办主任周蓉说，近段时间以来，市民对谷丰香油坊非法加工销售“大丰香油”事件给予了高度关注。3月10日，金牛区工商局和质监局就已介入调查。17日，成都市公安局对3名涉案人员实施了刑事拘留。26日，成都市公安局、质监局、工商局、卫生局经过调查取证，对违法事实进行了认证。<br />
<br />
经查，3名涉案人员为亲属和亲戚关系。刘磊，男，25岁，河南夏邑人，谷丰香油坊业主。大学毕业后随父亲刘志峰从事香油加工、销售，曾因妨害公务罪被金牛区法院判处拘役5个月。刘志峰，男，45岁，刘磊之父。在成都从事香油加工、销售。刘月光，男，24岁，河南商丘人，刘磊表弟。高中毕业后到成都投靠刘磊、刘志峰，从事香油加工、销售。<br />
<br />
违法事实香精+玉米油充麻油<br />
<br />
2009年3月，刘磊在金牛区工商局办理了《个体工商户营业执照》，经营范围为销售预包装食用植物油，未取得加工食用油的资质。<br />
<br />
谷丰香油坊的违法事实是，分别从成都郫县原野实业有限公司购进食用玉米油，从山东省滕州市润隆香料有限公司购进食用香精，以食用香精代替芝麻油，经过数次勾兑和掺合，非法加工成以假乱真的“芝麻香油”。<br />
<br />
谷丰香油坊冒用“成都原野实业有限公司”的企业名称和食品生产许可证号，伪造与加工地点不相符合的厂址以“大丰香油”名称进行销售。<br />
<br />
周蓉通报说，谷丰香油坊上述非法加工、销售“大丰香油”的行为构成了“无证无照生产经营”、“以假充真”、“伪造、冒用他人厂名”和“伪造、冒用他人生产许可证”的违法行为。成都市质监局、市工商局依法给予谷丰香油坊行政处罚。<br />
<br />
证据表明可排除“潲水油”嫌疑<br />
<br />
经公安、质监、工商部门现场搜查、勘验和对扣押的工具、原料等物品的仔细检查，均未发现与熬制“潲水油”有关的物品，走访现场附近群众也未有谷丰香油坊生产“潲水油”的反映。<br />
<br />
经审查涉案人员的加工制作程序，证据表明，可以排除谷丰香油坊生产销售“潲水油”的嫌疑，其不法行为定性为非法加工、销售食用油。<br />
<br />
据悉，成都市工商、质监、卫生部门对有关监管人员失职、失察行为，予以行政处罚。<br />
<br />
澄清误传13家火锅店名单公布<br />
<br />
经查实，购买使用“大丰香油”的火锅店共计13家。成都市卫生部门依据《食品安全法》对违规购买、使用“大丰香油”的13家火锅店，依法行政处罚。昨晚成都公布了违规使用“大丰香油”的13家火锅店名单，对社会上的误传予以澄清。<br />
<br />
针对之前媒体报道说，“成都市食安办的负责人说是‘潲水油’”的说法，周蓉也进行了澄清，她称，记者所指的市食安办负责人有误，他是成都市食品药品监督管理局的人员。<br />
