□答记者问<br />
<br />
“成都火锅店是让人放心的”<br />
<br />
什么是“潲水油”，什么是“地沟油”，为何“大丰香油”不是潲水油？<br />
<br />
发布会上，成都市食安办主任周蓉、成都市工商局公平交易执法局局长黄生、成都市公安局经济犯罪侦查处政委郑跃伟、成都市卫生局卫生执法监督支队副支队长李宏坤、成都市质监院食品检测中心主任万渝平回答了记者提问。<br />
<br />
为何不是“潲水油”？<br />
<br />
记者：为什么说“大丰香油”不是“地沟油”、“潲水油”？<br />
<br />
万渝平：俗称的“潲水油”是从餐饮行业的废弃食物残渣中提取的油。地沟油是地下水道漂浮的油腻物品中提取的物质。非法加工的食用油，是违反食品安全法，违反产品质量法，违反标准化法生产的食用油。<br />
<br />
从检测结果来看，查处的“大丰香油”以食用玉米油为主要原料，以食用香料勾兑调配而成。依据国家标准2716食用植物油的卫生标准进行检测，该油的指标符合卫生标准的规定。<br />
<br />
郑跃伟：调查中，没有发现该香油坊存在生产潲水油工具和原料。我再一次重申，不存在潲水油问题。<br />
<br />
成都的火锅还能吃吗？<br />
<br />
记者：现在市民都很关心，成都火锅店安全吗？<br />
<br />
李宏坤：成都火锅店是让人放心的。名单中的13家火锅店违规使用的不是有毒有害的油。按照《食品安全法》规定，成都的食品企业，经营时要查验供货商的检验报告，卫生部门长期以来要求餐饮业，包括火锅店进行凭证索票制度的建立。以后会把关，让餐饮业学会鉴别合法供货商。<br />
<br />
黄生：这次对全市拉网检查，对市场的4000多户食用油经营户进行了检查，结果除了50户的部分品种没有QS标识外，没有发现其他问题。我负责任地告诉大家，成都市场销售的食用油，广大市民可放心食用。<br />
<br />
为何之前政府没答复？<br />
<br />
记者：本次事件媒体早有报道，为什么政府部门一直没答复？<br />
<br />
周蓉：到27日才向媒体发通报，是因为需要调查取证。有些证据需要到山东滕州去取。这个过程中，各监管部门都要把有关搜查、勘验、取证工作做实在。<br />
<br />
记者石莉芳王浩野<br />
<br />
□联合暗访<br />
<br />
随机抽检5家店未发现“地沟油”<br />
<br />
昨日，四川省食品安全协调委员会办公室、省工商局、省卫生执法总队、省监察厅4家单位联合执法，对市内5家火锅店的食用油进行了检查。在5家抽检火锅店中，没有发现“地沟油”及“没有质检报告的食用油”。<br />
<br />
进门亮证件直奔后堂操作间<br />
<br />
由于此次检查属于暗访，省监察厅也被邀请来监督执法部门的检查。从火锅店食用油的进货渠道、台账、油的质检报告、熬制过程到潲水油怎么处理，全部被检查。<br />
<br />
上午10点，执法人员乘车出发。“去哪家店呢？”“生意好的大火锅店。”<br />
<br />
10：30，一行人来到琴台路一家火锅店。一进大门，执法人员掏出了工作证，并向接待人员说明了来意。接待人员说：“请各位等等，我马上通知经理过来。”“不用了，我们先进去看看你们用的油。”说着，执法人员直接绕过大堂走进操作间。<br />
<br />
火锅店的储货间里，堆着十几箱菜籽油及几桶香油。赶来的负责人一再强调，店里没有老油。当天剩下的潲水油，都有专门的公司进行回收。<br />
<br />
随即，他们拿出了一份协议书。根据协议，双流华阳镇一家个体养猪户负责每天回收潲水油。其中一条写着：“回收的潲水只能用于喂养使用，不能用于其它用途。”<br />
<br />
“你们去过这个人的养猪场没有？”<br />
<br />
“去过的，大概有500头猪的样子。”<br />
<br />
熬的什么油？边看边闻边问<br />
<br />
11点，执法人员走进了琴台路另一家连锁火锅店。一进门，就直奔操作间。这家店的油，是用透明塑料袋装好的，每锅一包。工作人员说，油是由总公司统一配送的清油。负责回收这家火锅店潲水的，是“成都市城卫保污油处理公司”。<br />
<br />
“有质检的检验报告。”操作间里，三口大锅里正在熬着油，锅面浮起一层油脂。执法人员仔细查看油的成色、闻油的味道。<br />
<br />
“这个是什么油？”<br />
<br />
“是牛油混着海椒熬的，锅底油。”正在掌勺的工作人员回答。<br />
<br />
被检查的几家火锅店都能提供所用菜籽油、香油的购货单及厂家质检报告的复印件。而火锅店也与专门的养猪场签了协议，由其专门负责处理潲水油。每一步检查，监察厅工作人员均在一旁监督。 记者王浩野</div>

							
							
							
							
