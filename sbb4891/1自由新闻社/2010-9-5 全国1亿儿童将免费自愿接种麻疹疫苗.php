<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 1974年，美国国家安全委员会出炉了高度机密（保密期15年te=mailto:GHOST dateTime=2010-03-20T17:10&gt;，te=mailto:GHOST dateTime=2010-03-20T17:10&gt; 1989te=mailto:GHOST dateTime=2010-03-20T17:10&gt;年解密）<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 的备忘录，题目是“世界人口增长对美国国家安全和海外利益的影响”。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 这份备忘录中涉及粮食政策、人口增长和战略性原材料。备忘录的内容是由约翰・D.洛克菲勒三世授意，尼克松亲自布置的。这个秘密的计划在华盛顿被简称为NSSM 200，即《国家安全研究备忘录第200号》。1975年杰拉德・福特一上任就立即签署了总统行政命令，使《国家安全研究备忘录第200号》成为美国政府的官方政策。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 《国家安全研究备忘录第200号》（NSSM-200）的主旨是实施“世界人口行动计划”───大幅度降低世界人口数量。这一新政策的理论出发点是：大多数高质量的矿藏都位于发展中国家，而发展中国家数量过多的“劣种人”妨碍美国获得充裕的、廉价的原材料，必须除掉这些高速增长的人口才能保护美国利益。基辛格明确提出：美国要将粮食援助作为“国家权力的工具”，对那些接受援助的国家来说，要么绝育要么挨饿。美国的新政策实际上就是“如果这些劣等人种妨碍我们获得充裕的廉价原材料，我们必须想办法除掉他们。”[注意：尼克松、基辛格都是美国共济会高层核心成员。]<br />
