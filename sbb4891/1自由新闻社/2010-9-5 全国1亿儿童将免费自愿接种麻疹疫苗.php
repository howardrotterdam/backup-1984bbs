&nbsp;&nbsp;<br />
&nbsp; &nbsp; 【美国共济会核心成员洛克菲勒家族与优生学】<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 自1920年起，洛克菲勒基金会资助在德国的优生学研究，通过柏林和慕尼黑的威尔海姆皇帝研究所（Kaiser-Wilhelm Institutes），直到第三帝国时期。他们赞扬希特勒德国的强迫绝育和纳粹的种族纯洁观念。　<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 约翰洛克菲勒三世是优生学的终生倡导者，从1950年起，通过他在纽约的私人人口理事会，他用自己的“免税”的基金发起了减少人口的新马尔萨斯运动。<br />
<br />
