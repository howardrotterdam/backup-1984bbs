&nbsp; &nbsp; 对于这种蹊跷的专门针对亚非拉和中国儿童的疫苗注射，必须参考以下世界形势和背景：<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 不久前，比尔・盖茨一鸣惊人地声称：新型疫苗和转基因食物是降低世界人口的好方法！<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 比尔・盖茨在美国加州长滩市（Long Beach）举行的TED2010会议上做关于碳排放的演讲，题目是《创新到零》（Innovating to Zero）。在讲话开始后4分30秒的时候，他说道：<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; “目前的世界人口有68亿，并且正在上涨到90亿。现在，如果我们能在新型疫苗、卫生保健、生殖健康服务方面做一些真正超级棒的工作的话，我们也许能降低10%~15%的人口。”────这段话的亮点是，盖茨公开提到：新型疫苗可以作为一种降低世界人口的方法。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 这次对中国全部儿童的免费麻疹疫苗注射来得蹊跷，有没有专业人士调查一下前因后果？？<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 1）此前我国有没有过在没有疫情的情况下，突然要求全国儿童限时全部接种的先例？<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 2）在没有事先专门的针对性准备的情况下，国内药厂应不应该有1亿只以上疫苗的储备量？<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 【疫苗和生物技术是美国在新世纪进行不对称战争的特种新技术】　　<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 1/免费疫苗和转基因种子的积极推动者是：<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 非洲绿色革命联盟(AGRA)。该组织成立于2006年9月，由<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 比尔和梅林达-盖茨基金会（BMGF）和洛克菲勒基金会（RF）提供资助。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 2008年英国的国际开发署（DFID）加入资助。前联合国秘书长安南任非洲绿色革命联盟主席。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 2/ 2009年5月5日，<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 在洛克菲勒大学校长保罗纳斯爵士的家中举行一次美国富豪高峰会议。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 这次财阀会议的中心内容是比尔-盖茨提出的议题：即如何更有效地推进生育控制和全球人口减少。会议达成的共识是，他们将支持“将人口增长视为对环境、社会和工业的潜在的灾难性的威胁的策略。”<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 3/美国财阀特纳、盖茨和巴菲特都是共济会高层会员，也是全球人口大规模减少项目计划的主要资助者。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 这些项目为非洲穷人提供医疗服务的慈善事业的面目出现。实际上它们涉及通过接种疫苗和施用其他药品，对人们进行非自愿绝育，使育龄妇女不育。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 20世纪90年代早期，世界卫生组织监督了在尼加拉瓜、墨西哥和菲律宾进行的大规模破伤风疫苗接种行动。疫苗中含有使女性无法维持妊娠的成分。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 世界卫生组织疫苗项目的破伤风疫苗只给15～45岁间的育龄妇女注射，没有给男性或儿童注射。此外，疫苗在数月内分三次注射，以此确保妇女的人绒毛膜促性腺激素维持足够高的高剂量，尽管一次破伤风注射至少在十年内有效。而所有接种破伤风疫苗的妇女都不知道该疫苗含有堕胎药剂。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 4/2001年9月，位于美国加州圣地亚哥的一家生物技术公司Epicyte宣布创造了转基因玉米的终极版本：“避孕玉米”，这种杀精玉米是对世界人口问题的一大贡献。当时Epicyte与杜邦公司(DuPont)及先正达公司(Syngenta)已达成合资协议以推广这一技术，预计该杀精玉米可于2007年前投入市场。值得高度关注的是，美国农业部支持和资助了该项研究！　　<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 2004年5月，美国一家生物科技公司Biolex收购了Epicyte公司，从而掌握了杀精玉米的转基因技术。从此以后，杀精玉米的话题从媒体上消失，变得讳莫如深！<br />
