													<h2>卫生部称麻疹疫苗已用44年均为国产 质量有保障</h2>
						
						
																											<div id="postmessage_819181" class="t_msgfont">　卫生部释疑麻疹强化免疫<br />
　　回应称疫苗已使用44年，质量有保障；均产自国内，不会增加不良反应<br />
<br />
　　本报讯 （记者吴鹏）9月11日起，我国将开展大规模的麻疹疫苗强化免疫。近日，有一些网友发帖对此次强化免疫麻疹疫苗的行为提出质疑，并对麻疹疫苗的安全性表示怀疑,称“麻疹疫苗是慢性毒药”。<br />
<br />
　　昨日，卫生部疾病预防控制局副局长郝阳辟谣，麻疹疫苗已使用多年，全部来自国产，质量是有保障的。<br />
<br />
　　昨日，郝阳表示，麻疹疫苗全部来自国产，而且质量是有保障的。此前，卫生部免疫规划专家咨询委员会主任委员赵铠曾介绍说，这次使用的麻疹疫苗的生产企业主要有上海、武汉、兰州和北京的企业，未使用进口疫苗。<br />
<br />
　　郝阳表示，麻疹疫苗从1966年开始就使用，疫苗质量有保证。对于网帖中质疑，“为何能一次拿出一亿支疫苗”以及“其他国家是否也有强化免疫的现象”，郝阳表示，此次强化免疫工作早已筹备，从去年至今，已准备了1.1亿多支。<br />
<br />
　　据了解，世界卫生组织将强化免疫定为消除麻疹的最有效策略之一。美洲于1989年至1995年期间进行了强化免疫活动。2002年11月以来，美洲区未发现有本土麻疹病毒传播。<br />
<br />
　　■ 释疑<br />
<br />
　　卫生部指出，这次之所以实施强化免疫，主要目的一方面是为既往未接种过疫苗的儿童、或因个体差异等其他原因免疫不成功的儿童提供一次补种的机会，从而使儿童产生有效的免疫力；另一方面强化免疫可迅速提高人群免疫力，形成免疫屏障，阻断麻疹病毒传播。<br />
<br />
　　已接种过疫苗的儿童为何要再次接种？<br />
<br />
　　麻疹疫苗首剂未成功须补种<br />
<br />
　　麻疹疫苗接种后，一般情况下个体可以得到有效保护。但也因人而异，接种后没有产生抗体的情况称为免疫不成功，这部分人在周围有麻疹流行的情况下有可能被感染。<br />
<br />
　　据介绍，通常情况下，麻疹疫苗在孩子8月龄接种首剂成功率为85%，在18月龄复种时血清抗体阳转率约为95%。完成2剂次接种的，血清抗体阳转率可能达到99%，但仍存在免疫不成功的人群。免疫不成功的人群累积到一定程度就可能导致麻疹局部暴发。<br />
<br />
　　强化接种免疫率为何要达到95%以上？<br />
<br />
　　95%人有抗体方可形成人群免疫屏障<br />
<br />
　　卫生部指出，对于人群来讲，需要有至少95%的人有抗体才可以产生人群免疫力，阻断麻疹病毒传播；对于个体而言，通过强化免疫再次接种麻疹疫苗，可以使未接种麻疹疫苗或接种后免疫无应答的孩子增加产生抗体的机会。<br />
<br />
　　卫生部表示，强化免疫为各种原因导致免疫不成功的人群提供了一次补种的机会，特别是对于免疫史不详或未完成2剂次免疫的人群尤为必要，因此国际上在开展强化免疫时，通常不考虑目标人群既往的麻疹疫苗免疫史，都接种1剂麻疹疫苗，以便建立起高水平的人群免疫屏障。据新华社电<br />
<br />
　　■ 北京情况<br />
<br />
　　北京疫苗产自兰州<br />
<br />
　　北京市卫生局昨日重申，按照卫生部的统一部署，北京此番麻疹强化免疫，使用的全部是政府集中采购的国产麻疹减毒活疫苗，生产企业为国药集团兰州生物制品研究所，采购总量为200万支。<br />
<br />
　　北京市卫生局疾控处处长谢晖介绍，目前，所采购的疫苗，以及配套的一次性注射器、安全盒、冷藏包等，目前已逐级下发至各区县主要居民聚居区的135家指定接种门诊。<br />
<br />
　　同时，卫生部门强调，无论是国产疫苗和进口疫苗，凡在国内用于免疫规划的，都须经过国家检测部门的严格审批、检验，质量和功效没有差别。我国与其他国家一样，使用的麻疹疫苗株均为A基因型麻疹病毒制成的减毒活疫苗。<br />
<br />
　　卫生部门已指定178家医疗机构建立疫苗接种后疑似不良反应的医疗救治绿色通道。<br />
<br />
　　本报记者 魏铭言<br />
