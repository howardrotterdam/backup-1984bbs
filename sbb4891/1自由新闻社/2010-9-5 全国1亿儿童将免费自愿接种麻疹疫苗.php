作者：何B&nbsp; &nbsp;来源：每日经济新闻<br />
<br />
　　“我们家儿子接种那个麻疹疫苗了吗？网上说那个疫苗是‘慢性毒药’，先不要去接种啊。”昨日（9月6日），正在上班的孙女士接到孩子父亲打来的电话，焦急万分地向她说了这则“骇人”的传闻。<br />
<br />
　　上述麻疹疫苗，指的是卫生部于9月1日宣布即将开展的全国范围的麻疹强化免疫活动。强化免疫的宣传刚起，网络上便开始流传关于此次接种麻疹疫苗不安全的种种言论，甚至有消息称，此次疫苗为外国赠送的“慢性毒药”。<br />
<br />
　　对此，昨日卫生部连发六文表示，本次全国麻疹疫苗强化免疫所使用的疫苗，均为国产疫苗，所有疫苗均为经过中国生物制品检定所每批检测合格的疫苗。而且，多次接种麻疹疫苗不会增大不良反应发生的概率。<br />
<br />
　　卫生部同时表示，中央财政已安排1.57亿元的疫苗和注射器经费，向国内企业招标采购。<br />
<br />
　　疫苗=慢性毒药？<br />
<br />
　　9月1日，来自卫生部的消息称，本月11日至20日，我国将统一开展一次以8月龄至14周岁儿童为主要接种对象的强化免疫活动，预计有1亿名儿童将免费接种麻疹疫苗。<br />
<br />
　　但接种工作尚未展开，网络上已开始广泛流传关于“北京大学医学部免疫学系的有关专家”质疑此次疫苗接种的言论。其多篇博文被各大论坛广泛转载。随后，某知名人士也在自己的博客中转贴了前述专家的部分观点，并质疑“联合国为何要对中国儿童蹊跷注射疫苗”。<br />
<br />
　　除了网络传播，还有部分市民接到短信，称此次疫苗是“外国赠送的产品，完全可能是慢性毒药”。<br />
<br />
　　昨日，记者发现，该知名人士的博客已经关闭，而前述专家博客中的大部分相关博文也显示 “已隐藏”。但由网络引发的这场质疑已经扩散开来，记者发现许多网民已经注意到这则传闻，并对此议论纷纷，有不相信的，也有表示担心的，更有部分家长则直言宁可信其有，坚决不让孩子接种疫苗。<br />
<br />
　　卫生部辟谣：疫苗全国产<br />
<br />
　　针对上述传言，昨日，卫生部在其官方网站上连续发布了六条关于麻疹疫苗强化免疫的文章。<br />
<br />
　　其中特别强调，本次全国麻疹疫苗强化免疫所使用的疫苗，是由各省按照 《中华人民共和国招投标法》规定的招标采购程序向国内具有生产资质的企业招标采购的国产疫苗，所有疫苗均为经过中国生物制品检定所每批检测合格的疫苗。<br />
<br />
　　“国内外的研究均表明，多次接种麻疹疫苗不会增大不良反应发生的概率。”<br />
<br />
　　卫生部表示，此次实施强化免疫的主要目的一方面是为既往未接种过疫苗的儿童、或因个体差异等其他原因免疫不成功的儿童提供一次补种的机会，从而使儿童产生有效的免疫力；另一方面在短时间内对特定人群开展麻疹疫苗强化免疫可迅速提高人群免疫力，形成免疫屏障，有效阻断麻疹病毒传播。<br />
<br />
　　卫生部还指出，本次强化免疫对儿童家长不收取任何费用，疫苗及注射器费用均由国家财政负担，为此中央财政已安排1.57亿元的疫苗和注射器经费。<br />
<br />
　　据介绍，本次麻疹疫苗接种采用自愿原则，卫生部门将给家长一封《接种通知单》，在9月11日至20日期间，家长可按照“麻疹疫苗强化免疫活动接种通知单”预约日期，主动带孩子到指定接种点接种。<br />
<br />
