													<h2>曾子墨父亲曾庆瑞直言《爱情》低俗 赵本山恼羞成怒</h2>
						
						
																											<div id="postmessage_567314" class="t_msgfont"><a href="http://tieba.baidu.com/f?kw=%D4%F8%C7%EC%C8%F0" target="_blank">http://tieba.baidu.com/f?kw=%D4%F8%C7%EC%C8%F0</a><br />
先看曾庆瑞贴吧，赵本山的马仔在那里撒泼了<br />
<br />
<br />
<br />
据《羊城晚报》报导，前天（4月12日），中共国家级“丑宝”赵本山在北京为自己的电视剧《乡村爱情故事》召开研讨会。一开场，赵本山不但挨个儿与到场的专家、学者、记者握手致谢，还姿态谦虚地向各位求教。赵本山一再要求与会者向他说真话，不留情面地批评，他说：“在公司，周围的人天天都说好话，我今天就想听‘坏话’和真话。我永远感谢让我经受磨难的人和给我批评的人。”<br />
　　<br />
不料，被赵本山的“诚恳”和“鼓励”感动的中国传媒大学教授曾庆瑞教授直言“本山喜剧是伪现实”后，赵本山恼羞成怒，大骂曾教授的直言是“毒药”，而他赵本山“要良药不要毒药”，愤然声称：“我敢说，农村生活在座的各位没有比我更了解的，我是你们的老师！”令《乡村爱情故事》研讨会不欢而散。<br />
曾庆瑞教授三点直言<br />
<br />
收视误导──“本山先生被收视率带来的鲜花、掌声给弄迷糊了，被某些不负责任的言论、没有原则的吹捧给误导了。”<br />
　　<br />
缺乏真实──“《乡村爱情故事》展现了农民生活的很多场景、片断，但缺乏历史进程中本质的真实，其中塑造的人物形象扁平化、不够典型，没有时代背景下共同群体的特征。”<br />
　　<br />
低级趣味──“真正的喜剧应当以现实社会中的矛盾为基础生发出来，而非像《乡村爱情故事》等剧所展现的那样，放大人物的身体缺陷(如结巴)。这样博得的笑声缺少爱和悲悯的情怀。本山先生不缺乏技巧，但更重要的是要追求更高尚的境界和更博大的情怀。艺术家应当以追求高雅、崇高为目标和境界。”<br />
<br />
赵本山恼羞成怒<br />
<br />
面对曾教授的直言，赵本山恼羞成怒：<br />
<br />
1、我从来都不是高雅的人，也从来没装过高雅，我也最恨那些自命不凡、认为自己有文化的、实际在误人子弟的一批所谓教授。高雅是从民俗进化来的，没有大俗就不会有大雅。 <br />
<br />
2、我不是拒绝高雅。我们都想高雅，但我在高雅这块地上没有活过，我的生活圈子(就是这样)。我也想高雅，直接就进入宫廷，但它不是我的生活。这位教授提到的农村生活，我想知道，您熟不熟悉农村生活？农村到底什么样？您去没去过？您体验过吗？如果没有发言权的话，那考虑好再说。 <br />
<br />
3、我来是想找一服对我有好处的药，别给我开一服吃了就死的。我想找一个好医生，不是假医生。我想吃良药，不希望吃毒药。 <br />
<br />
4、我敢说，农村生活在座的各位没有比我更了解的，我是你们的老师，就不要唠农村了，如果唠城里的事，我赶紧缴枪不杀。 <br />
<br />
5、不强调收视率的存在，就是污蔑存在的价值，电视为谁做的？为高雅做的吗？是为大众、为百姓做的。不如您自己写个剧本，自己拍一个，如果您拍的那个收视率比这个高， 我当时就给您跪下。<br />
<br />
6、我觉得，如果一个人能把全国那么多观众弄乐了，就是最大的慈悲和善良。<br />
<br />
<br />
网民炮轰赵本山<br />
　　<br />
赵本山拙劣表演遭到了网民的炮轰。有网民讽刺道：“老赵的东西严格讲不是艺术，而是以市场为导向的低俗搞笑产品。教授的艺术批评是对牛弹琴，得不到老赵的认同是可想而知的。源于生活高于生活的是艺术，源于生活低于生活是老赵。不要埋怨老赵没有宰相肚子，要埋怨教授自己没有眼光，把宰相府的一个小丑当成了宰相。”<br />
<br />
有网民直言：“我看了《乡村爱情》几集就不看了，导演好像实在是拿不出来东西了，就拿农村人开涮。凡是在农村生活过、了解农村的人，都会对故事内容的夸张过度有意见。难道说农村就是残疾人、结巴子、有精神障碍的人的地方？全中国有几个农村的人系领带？从心里抵触这部电视剧。”<br />
　　　　<br />
也有网民指出，赵本山这一对批评的假欢迎太精彩了，可以说再现了新中国六十年的专制术――1957年反右前，毛泽东就是这样诚恳地动员知识份子帮助党整风，待知识份子直言批评时，突然翻脸反右，将几十万人才戴右派高帽流放劳改。1979年，邓小平高呼解放思想民主法治，却将在“民主墙”上提意见者送进监狱；1989年，更是以坦克碾压天安门请愿解放思想的学生……赵本山自从1990年第一次上春晚后，早已变成了黑白两道通吃的助纣为虐的土皇帝。<br />
　　<br />
还有网民抨击道，赵本山本想像中共组织假欢迎一样召开《乡村爱情故事》研讨会骗取赞誉，不料有不识趣者要真批评，他只得暴露自己对于批评只是假欢迎，凶相毕露。这就正式宣告赵本山死了！现在有着私人飞机的专制帮凶“赵总”只会让全国百姓莫大愤怒！<br />
