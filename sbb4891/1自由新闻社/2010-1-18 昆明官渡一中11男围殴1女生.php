倒数计时 木棍打头 轮番脚踢<br />
<br />
第3分钟的时候，徐婷婷有过短暂的抬头，随后，她低头沿河岸默默往前走，“摄像大哥”一直在她前面拍摄。3分58秒时，徐婷婷发现前边站了一大堆男生，她抬头怯生生地望了两眼，停下脚步。<br />
<br />
男生们围上来，有男生开始倒计时：“10、9、8……”徐婷婷再次被男生们包围，当倒计时数到“6”的时候，最开始动手打耳光的那名男生从徐婷婷身后飞腿踢了上去，徐婷婷被踢倒在地上，白色校服上沾满了泥巴。她爬起来，在男生们的再次倒计时中，穿红色衣服的男生右手持棍棒，倒计时数到“1”的时候，这名男生将木棍朝徐婷婷的后脑勺抡了下去，并呵斥着：“你还不走？走不走？”<br />
<br />
在哄笑和棍棒声中，徐婷婷开始一路向前小跑，男生们一路紧追。因奔跑的缘故，随后的视频显得十分混乱。5分04秒，视频再次清晰时，徐婷婷已经低着头坐在地上，至少有6名男生开始轮番用脚踢，徐婷婷被踢倒在地，滚来滚去，她用围巾擦拭自己的眼睛，后背被踢了一脚又一脚，红衣男生还上前揪她的头发，随即用脚踢她的头。有的男生用肘子，有的男生用脚……这个过程中，徐婷婷一言不发，默默忍受。<br />
<br />
红衣男生大叫一声：“让开！”几个大步，将脚跟狠狠地踢在徐婷婷的背脊上。“一个一个来，排着队形！”紧接着，男生一个接一个地上前踢打――这个残酷的过程延续到第6分38秒。<br />
<br />
6分38秒―结束：<br />
<br />
推来推去 飞腿袭身 踢进水里<br />
<br />
第6分39秒时，徐婷婷已经站了起来，有男生说：“……回去不好交代！”“洗了，洗了！”徐婷婷再次被男生们围住，有男生侮辱说：“她不男不女的，脾气又倔！”之后，徐婷婷又被推来推去，男孩子们的飞腿不时落在她身上，8分55秒，徐婷婷被大家逼到河岸，被红衣男生一脚踢了下去。和第一次落水一样，徐婷婷在水里蹒跚着前进，河水漫过她的腰部，岸上一片放肆的笑声，“10米跳台跳水！”。<br />
<br />
视频最终在一名男生的一句“在下面好好洗澡，没有哪样事情了！”中结束。<br />
<br />
在这9分46秒的时间里，徐婷婷没有哭，也没有太多反抗，只是偶尔一声怒斥，从语气听，她说的话大多近似商量。<br />
<br />
各方反应<br />
<br />
班主任“很愤怒” 警方再次调查<br />
<br />
经查实，被多名男生百般侮辱的女生徐婷婷系昆明市云秀书院官渡一中初二的一名学生，参与殴打、谩骂她的男生中，大部分为初三（8）班学生。据初三（8）班的男生介绍，事情发生后，该班一名姓马的男生因参与侮辱师妹被开除。初三（8）班班主任侯琼芳则解释，马同学并没有被开除，只是让家长领了回去。<br />
<br />
徐婷婷的班主任刘老师说，徐婷婷已经有三天没有上学了，但和被殴打、侮辱并无直接关系。刘老师本人也看到了这段视频，并表示看了之后“很愤怒！”一名初一的同学透露，这件事情早在同学当中传开，初中部各班级的班主任在课堂上对学生进行训诫，要求同学们引以为戒，不要有类似的事情发生。初三（8）班的一名男生坦言，这段视频在校园内流传开来的时候，他“不敢正眼看，太残忍”。他说，除了姓马的学生被开除外，另外两名分别姓昌和姓甘的同学也参与了施暴。<br />
<br />
官渡一中领导说，该事件发生的时间是去年11月底。从事情发生的第二天开始，官渡派出所、学校保卫科就开始对这起事件进行调查。由于当时并没有视频，徐婷婷的父亲、校方和警方均认为这不过是一般的校园斗殴事件，本着保护、教育未成年人为主的目的，此事只不过在学校做了一次通报，并对当事学生进行通报批评。<br />
<br />
昨日，官渡派出所所长张闻波一脸凝重地看完了这段视频，“不对，这个事情不对！”和张闻波一起观看了这段视频的另一名工作人员也是一脸愤怒。看完视频后，张所长立刻带领几位警员，驱车前往学校，“这个事情必须再次调查！”张闻波表示，此事必须通报给官渡区教育局。<br />
<br />
女孩人缘不好 经常与人斗殴<br />
<br />
“她性格很像男生，挺瞧不起人。”跟徐婷婷同住关上某小区的一名初一女生说。<br />
<br />
“在班上的人际关系也不好，我们都不怎么跟她处，她在班上成绩也很差。”徐婷婷的同班同学对她做出了如此评价。初三（8）班的刘芸（化名）和徐婷婷做了8年朋友，刘芸说，徐婷婷的脾气越来越怪，得罪了很多同学，和徐婷婷有关的斗殴事件非常多，有时候是她打别人，有时候是别人打她。<br />
<br />
刘芸说，在学校里大多时候都是她在“罩”着徐婷婷，她认为徐婷婷平时不太会说话，“喜欢得罪人” ，徐婷婷被打当天她也在现场，最后还是她和班上的另一名男生裘志（化名）好心地把徐婷婷从水里拉出来的，然后他们送徐婷婷回学校，并让她的爸爸来接她， “我和她做了八年的朋友，但现在不是了！”刘芸说这句话的时候显得有些无奈：“我好心救她，最后她还反咬我一口，说是我那天也打了她。”刘芸说，不久前，徐婷婷找四个人打了她一顿，后来怕她找人去报复她，所以徐婷婷不敢来学校了。<br />
<br />
裘志说，他当时也踹了徐婷婷一脚，但却是被本班的马某逼的， “他们说，如果我不打徐婷婷，他们就打我”。裘志踹了徐婷婷一脚后就离开了，之后，他才和刘芸一起去把徐婷婷从水里拉上来。裘志说，如今他为自己的行为感觉后悔，所以他愿意将那段经历详尽说出，以减轻自己内心的愧疚。裘志带我们来到徐婷婷被踢下河的地方，这里是季官村旁的老宝象河，两边是高高的堤坝，大概三四米左右，站在河堤上可以看到河底和水里的水草，水深大概半米，“那天徐婷婷被拉上来以后，称自己没事”。<br />
<br />
女孩父亲表示 女儿也有不对<br />
<br />
徐婷婷的父亲并没有看到这段视频，但他说，当时从孩子的穿着看，孩子肯定遭到了凌辱。事情发生后，徐先生就拿着孩子沾满红泥巴的脏衣服和鞋子到学校，要求给一个合理的解释，“当时很心痛，很不舒服！”后来，他又带孩子去医院做了一些常规的检查并拍摄了相关照片，“医生说小孩太小，有的检查不能做，只能靠自己去发现，当天检查出大部分是外伤，小部分内伤”。<br />
<br />
“徐婷婷是我的独生女，现在独生子女的脾气、个性都很另类，我对自己的孩子非常了解，发生这样的事情，首先她也有不对的地方。”徐先生说，到了派出所后，他意识到让孩子到派出所去可能对她的成长不利，“当时我想，学校是教育的地方，小孩子即使做错事，也应该有改正的机会。班级学生多，老师也很辛苦，况且这件事情的发生有很多不确定的因素”。<br />
<br />
徐先生承认，徐婷婷的学习和生活已经受到了这件事情的影响，“她有点害怕上学，我正在做她的工作。她的性格有点孤僻，我也为此烦恼。”徐先生说，事情已经发生了一段时间，他不愿意过多追究，也不愿意再次看到那段视频，“作为家长，将自己的孩子教育好，这才是最重要的”。徐先生说，孩子短时间内可能仍不会上学，因为她并没有做好足够的心理准备，“我正抓紧做她的工作，该上完的课程一定要上完”。<br />
<br />
昨天徐婷婷仍没有上学，谁也不知道她究竟什么时候才愿意回到校园。<br />
