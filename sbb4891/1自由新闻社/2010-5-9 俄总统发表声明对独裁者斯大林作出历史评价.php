													<h2>[转]俄总统发表声明对独裁者斯大林作出历史评价</h2>
						
						
																											<div id="postmessage_843920" class="t_msgfont">贴这个文章是由于有时会被网络的&quot;假&quot;新闻忽悠。 于是今天心血来潮，忽然想实证的查一下新闻。觉得，即便是不懂那么多语言，想要大概证实一下还是不太难。于是顺手将结果分享到这里。如果大家都能将可疑新闻证实一下，那么将大大提高新闻的信服力。<br />
