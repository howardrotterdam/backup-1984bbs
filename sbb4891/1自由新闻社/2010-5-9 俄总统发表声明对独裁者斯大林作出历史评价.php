原文链接: <a href="http://www.izvestia.ru/pobeda/article3141617/" target="_blank">http://www.izvestia.ru/pobeda/article3141617/</a><br />
<br />
---------------------<br />
俄罗斯总统梅德韦杰夫5月7日声明说：是人民赢得了伟大卫国战争，而不是斯大林，并表示无论斯大林取得怎样的工作成绩，他对人民犯下的罪行是无法饶恕的。<br />
<br />
&nbsp; &nbsp; 在俄罗斯即将举国庆祝5月9日“胜利节”前夕，俄罗斯社会针对如何评价苏共党魁斯大林问题的争论仍无法平息，梅德韦杰夫星期五亲自就该问题发表声明，成为首位针对前苏联时期独裁者斯大林作出了历史评价的俄罗斯总统。<br />
<br />
&nbsp; &nbsp; 梅德韦杰夫在接受俄罗斯著名《消息报》采访时，就应该如何评价斯大林问题表示说：“如果谈到应如何评价斯大林时期的统治这个问题，那么实际上从新的俄罗斯诞生以来，这个评价就已经是显而易见的了 ---- 斯大林针对自己的人民犯下了滔天罪行。虽然说他在管理国家方面做了很多工作，虽然在他的统治下，前苏联取得了一些成绩，但他对于自己的人民所犯下的罪行是无法饶恕的”。<br />
<br />
&nbsp; &nbsp; 近期，一些俄共人士要求莫斯科市政府在胜利节期间于莫斯科大街小巷悬挂带有斯大林画像的宣传画，称斯大林领导了二战的胜利，而且斯大林统治时期的苏联在重工业、农业各个方面取得显著发展，所以虽然他迫害了一些人，但仍功不可没。<br />
<br />
&nbsp; &nbsp; 俄总统就此表示说：“非常显而易见的一点是：伟大卫国战争是人民赢得的，而并不是斯大林，也不是那些军方高官，无论他们所从事的事情多么重要。” 他在声明中还说：俄罗斯已经在走向明天，昨日的斯大林主义不会在俄罗斯复辟。<br />
<br />
&nbsp; &nbsp; 有评论人士表示：俄共人士评价斯大林的态度同中国共产党历来的三七开评论方法类似，似乎只要给了百姓生存权，还给了百姓一些本属于人民的权利，那么所有一切政治和信仰迫害以及违反人权的罪行就可忽略不计，功过相抵…… 但此次俄罗斯总统就此问题发出了鲜明的最强音：国家的发展是人民大众的功绩，独裁对人民犯下的罪行无法饶恕。三七开的评论方法故意模糊了道德底线，而独裁超越了道德底线的犯罪是没有任何理由能辩解的。从某种意义上讲，推翻了苏共统治后的俄罗斯的总统针对斯大林问题的声明对中国人民更具现实意义。<br />
<br />
&nbsp; &nbsp; 俄罗斯评论人士对梅德韦杰夫的声明给予了积极评价。《消息报》主编阿波拉莫夫对此表示说：“也许人们到将来才会真正明白俄罗斯总统此次声明的真正意义。总统的声明给了社会再次前进的机会，因为如果在应如何评价过去这些问题上都模糊不清的话，就很难明确未来的发展方向”。<br />
