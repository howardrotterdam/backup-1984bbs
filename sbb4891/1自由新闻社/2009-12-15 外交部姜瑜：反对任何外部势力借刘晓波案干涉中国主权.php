[日期：2009-12-18] &nbsp; &nbsp; &nbsp; &nbsp; 来源：参与&nbsp;&nbsp;作者：沧海 &nbsp; &nbsp; &nbsp; &nbsp; [字体：大 中 小]<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;（参与2009年12月17日讯）沧海报道：中国外交部12月15日驳斥美国与欧盟呼吁中国释放异见人士刘晓波的要求，并警告外界勿干涉中国内政和司法主权。就中国政府在此事上的强硬立场，《参与》记者12月17日先后采访了同为《零八宪章》联署人的“六四四君子”之一周舵先生和资深媒体人李大同先生。<br />
<br />
周舵认为这是中国政府一贯的做法，从来如此，没有什么可奇怪的，因为一党专制的政权不受监督和约束，当然是想怎么干就怎么干，最可怕的是在全球经历经济危机的时候，美国一些过去以人权斗士著称的政治家都跑到北京来跟中国政府磕头了，国内、国外几乎对中国政府没有任何制衡的力量。宣布刘晓波无罪的可能几乎没有，对于判到什么程度很难下结论，中国的法律完全是个橡皮尺子，不是依法办事，而是根据当局的政治需要，取决于掌握决策权的人怎么判断形势，怎么权衡利弊，怎么做政治上的成本收益分析，没有客观尺度就没办法预计。中国政府虽然在若干年前就提出建设法治国家，但只是一个长期的目标，不能说没有做这方面的努力，比较公正地说还是往这个方向一步一步走，政治案件是一个特殊领域，可以说是一个法外的领域，法制的阳光基本照不到这个角落。<br />
<br />
周舵表示，全世界有良知的人都应该尽自己的所能去向中国当局施加压力，尽管有没有效果没人能知道，这个压力毕竟还是太小。他预测当局不太可能让去法院陪审，可能连家门都出不去，他举例说六四二十周年前夕，他严格按照法律规定去申请静坐绝食，结果在家门口就被堵住了，根本到不了提交申请书的地方。关于《零八宪章》成为起诉刘晓波的罪证之一，他说，这要看怎么援用法律，法律是不是正义的法律，是良法还是恶法，不是立法机构立的法律大家就必须遵守，即使法律是正义的良法，但如果没有一套机制保证法律的执行，只是选择对执政者有利的执行，也谈不上是法治国家。《宪法》1954年就规定了公民的一系列的权利，到现在一项也没有实现，《零八宪章》的联署人并不指望提出的要求很快能实现，只不过提出一个大家应该努力的目标，只有足够多的人为这个目标去努力才会一步一步实现，恐怕还要一个很漫长的过程。目前的问题是，不管提出的意见是正确还是错误，至少要保障公民发表不同意见的权利，现在连说话的权利都没有，只能说和毛时代相比有一些进步。<br />
<br />
&nbsp; &nbsp; 李大同认为中国政府在逮捕刘晓波一事上违反了自己制定的宪法。刘晓波就是坐在家里，说了几句话，表达一点自己的意见，天底下凡是有起码常识的人都能作出判断，中国政府还谈什么司法，所做的一切都是违法的，而且违反自己制定的法律，这是没有廉耻，没有文明底线的表现，由此也可以看出中国政府有可能在这件事上要走到底。现在联署人能做的无非就是表达，交给历史去记录。他希望当局迷途知返，不要在历史上再留下这么一个恶劣的记录，自己羞辱自己。谈到对中国人权状况的展望，李大同说，“慢慢来吧，中国的事情不是一夜可以发生改变的，需要历史性的改革的阶段，大概需要几十年的时间。中国的人权状况只能有所进步，虽然不是当局想做的，但是民众已经觉醒了，时代已经不一样了。”</div>

							
							
							
							
