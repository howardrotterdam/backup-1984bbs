<br />
2009年 12月 16日<br />
记者: 高勒斯特 | 华盛顿 <br />
<br />
美国国务卿希拉里.克林顿再度呼吁中国立即释放持不同政见人士刘晓波。刘晓波是中国作家，同其他人签署了“08宪章”，在去年被捕。克林顿国务卿星期一的讲话，谈到了美国的人权政策。她说，尽管美国还有其他方面的利益，但还是要向中国和俄罗斯这样的大国表达美国对人权问题的关切。<br />
<br />
*连续两天提到刘晓波*<br />
<br />
美国国务院连续第二天提到了中国作家刘晓波的案子。中国警告说，外界要求释放刘晓波，是干涉了中国的内政以及司法权。<br />
<br />
刘晓波曾是北京师范大学的讲师，已经坐牢一年了。这是因为他起草并签署了08宪章。该宪章呼吁中国进行政治民主改革，尊重人权。<br />
<br />
*高度关切*<br />
<br />
美国国务院主管公共事务的助理国务卿克劳利给参加记者会的人散发了一份声明。声明说，刘晓波在12月10号国际人权日被正式起诉，罪名是颠覆国家政权。美国对此表示高度关切。<br />
<br />
克劳利说：“根据所有能得到的公开证据，对刘晓波的起诉是建立在这样的基础之上的，那就是：他签署并支持了08宪章。而08宪章只是要求尊重人权，普世人权和进行民主改革。”<br />
<br />
