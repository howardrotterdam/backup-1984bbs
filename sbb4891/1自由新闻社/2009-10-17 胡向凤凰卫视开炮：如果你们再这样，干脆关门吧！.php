<br />
博讯记者在获得这一消息后可谓半信半疑，按说，在党内分工中，胡锦涛是主管港澳这一块，包括新闻媒体。但胡锦涛做事一向低调，而且对一个媒体的主管说如此重的话，似乎不太可能。记者推测是某些媒体的主管为了抬高自己的身价而编造出来的。所以，博讯记者和编辑并没有及时报道这一消息。然而，国庆前后，博讯住香港和北京的记者获得了更多消息，不但证实确有此事，而且，还有更深内幕。 (博讯 boxun.com)<br />
<br />
博讯记者在国庆节期间采访60周年国庆大典时获知，国庆节前被胡锦涛亲自约见的港澳媒体负责人远不止刘长乐一人，除了中资背景的媒体，连港资的媒体老板也被接见了。说是接见，其实是训话。有些以前见过胡锦涛而且有些交情的负责人觉得很反常，胡锦涛显得很急躁，甚至有些气急败坏，不但有失风度，也失去了常态。<br />
<br />
据博讯记者获得的消息，胡锦涛对港澳媒体发火与江泽民卷土重来，要钦定下一代接班人有关。虽然目前中共18接班态势还没有明确，但最有可能接班的习近平、薄熙来两人都是江泽民的亲信。特别是黑马薄熙来这次出够了风头，也给中央很大的压力。(boxun) <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 就是薄熙来在重庆搞红段子，大肆宣传革命传统和老一辈无产阶级打江山的事迹的时候，中央宣传部门接二连三接到对港澳媒体（包括凤凰卫视）的指控，认为这些长期得到中方支持甚至赞助的媒体已经自由到“肆无忌惮”的地步，应该追究他们背后的黑手。这背后的黑手呼之欲出，自然是主管港澳事务的胡锦涛。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 这件事得到胡锦涛的高度重视，因为他已经感觉到自身的危险。胡锦涛作为邓小平隔代指定的接班人，从来没有得到江泽民的支持（当然也没有激烈的反对）。江泽民在位十几年，下台后一直提心吊胆，就是担心这位不是自己指定的接班人突然搞什么“改革”，不但把他江家的家族利益“改革”掉了，甚至会把他江泽民推向历史垃圾堆。所以，江泽民的最大愿望就是要介入胡锦涛之后的接班人，再确保下一代八年执政，他仍然可以高枕无忧。而且目前看来，江泽民的这一愿望也即将实现。(boxun.com)<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 胡锦涛之所以被邓小平看上，很大的原因之一就是个人“品格正直”，在党内从来不搞宗派（至少在那时），这一性格在他上台后仍然很明显，和江泽民在位几年几乎把中央全部换成了“江家班”不同，胡锦涛用人比较慎重，并没有使用清一色的自己的人。有人误以为胡锦涛因为出身团派，就把所有的团派人马归为他的人，其实不然，团派并不时铁板一块，里有很多是江泽民的人。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 然而，胡锦涛的这一人品“优点”却也给他造成了危机，还有三年就要下台的胡锦涛现在不能不考虑自己在台上的时候是否得罪了什么人，下台后是否有人要“变天”，要和他算帐。从江泽民咄咄逼人的态势来看，上台时给人很大希望的胡锦涛才是真正是过渡人物。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 由于形势很明确了，所以江泽民在十八大前频繁亮相，已经不再掩饰自己对人事布局的“关心”和干涉。据博讯记者获得的确切消息，十八大后，江泽民势力将会卷土重来。一代领导人胡锦涛讲会像朱F基这些领导人成为昙花一现的过渡人物。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 胡锦涛也越来越明白了这一道理，好在他在位期间，还是做了一些好事，而且手上也并没有像江泽民那样沾了很多血。但从他对于江系人马对港澳媒体的指责如此紧张来看，他也有自己的“政治觉悟”和恐惧。<br />
&nbsp; &nbsp; <br />
