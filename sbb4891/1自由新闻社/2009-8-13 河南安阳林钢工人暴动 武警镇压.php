													<h2>转：关于林钢目前形势及几项重点工作意见</h2>
						
						
																											<div id="postmessage_201894" class="t_msgfont">“维护稳定，恢复生产”！这是近段以来广大干部职工、离退休人员、社会各界人士对林钢的一致要求和呼声。只有恢复正常生产经营和工作秩序，才能最大限度地维护职工的切身利益。在事关林钢生死存亡及广大干部职工就业和生活面临严峻挑战的情况下，我们决不能坐以待毙，更不能旁徨、等待、任其事态继续发展。我们必须振作精神，团结起来，克服一切困难，采取各种有效措施，使林钢尽快扭转局面，更好地生存和发展下去。<br />
一、由于生产瘫痪，许多该办、想办的事，办不成、无法办。近两个月以来，国家对钢铁企业的信贷支持，与我们林钢擦身而过却办不成。原与几家金融单位达成的融资贷款意向，也被无故中止。<br />
二、部分干部职工思想混乱，情绪不稳，士气低落。就是这段时间一直坚持值班的人，也倍感困惑和无所适从，多数职工对林钢的前途和命运十分忧虑。四是受上述因素的影响，林钢在外界的整体形象和企业诚信度大打折扣，已造成不可挽回的严重后果。<br />
三、关于企业与职工发生的款项问题。近几年，由于受国家宏观经济调控和产业政策的严重制约和影响，在银行信贷和社会融资相当艰难的情况下，依靠干部职工融资，建成了高炉喷煤工程、电厂二期工程、450m3高炉工程等重点项目，确保了林钢持续生存和发展。在集资款、入股金的兑现上，公司一直抱着良好的愿望，承诺过，强调过，也始终把确保企业正常生产和林钢生存看作是天大的事，觉得这就是职工最大的根本利益。大家也都知道，2007年12月30日才建成3号高炉工程；2008年上半年受安林高速安林公路环境综合整治的影响，被多次停电、停产，直至2号高炉整体拆除。<br />
下半年受大宗原燃料涨价、生铁市场滞销以及全球金融危机的影响，被迫采取限产措施。期间，还有连续发生的几起重大设备事故对生产的影响。致使3号高炉全年停产182天、1号高炉全年停产289天、2号高炉仅生产18天，企业生产经营损失惨重。在这样的处境下，考虑到维持正常生产的资金链问题，把所欠职工的应付款项延缓推迟了。现在看来，公司的想法和职工和想法还是有差距的，必须把职工利益和维护稳定放在最重要的地位。因此，对于大家提出的相关资金事宜，已经公司办公会议研究，决定在资金许可的情况下，分期、分批发放。目前，风险金、宏达公司分红、电厂二期借款、独生子女费已发放完毕。随后，职工入厂费、路通公司分红、3号高炉资金等涉及职工的应付款项也要尽快发放。<br />
&nbsp; &nbsp; &nbsp; &nbsp; <br />
积极配合市委工作组的工作。针对一部分职工在濮阳期间提出的有关问题，濮阳市委高度重视并及时召开紧急常委会议，作出：林钢改制工作暂缓进行，待条件成熟后再实施；责成组织和审计部门对林钢领导班子进行年度考评和审计；对职工提出的利益方面的事要制定方案，最大限度地确保职工利益。根据市委的指示，对林钢领导班子考评、审计工作正在进行。各单位要积极配合，做好工作。<br />
恢复生产是当务之急。首先大家知道，林钢3000多名职工，乃至上万名家属赖以生存的物质条件就是生产生铁产品。现在高炉停产，就意味着职工的收入没有了。所以，恢复高炉生产是当务之急。但一些职工认为，高炉生产也是赔钱，还不如不生产好。在这个问题上，公司责成有关部门和人员进行了认真分析和精心测算。<br />
以 2008年为例，上半年受外部干扰，下半年受市场原因和主要设备出故障等原因，使高炉停开频繁，生铁产量很低，全年生产生铁仅18.4万t（上半年生产 12.5万t，下半年仅5.9万t），占年正常生产能力的50%左右，因此使生铁成本居高不下。生铁制造成本2008年上半年平均4371元/t，下半年平均4354元/t，全年平均4366元/t。由于二季度大宗原燃料价格和生铁售价上升到最高峰，三季度受市场影响，价格突降（生铁价格由5100元/t 降至2009年初3150元/t），但库存高价位原燃料和库存高价位生铁都要由以后各月份分别承担。<br />
2008年下半年至现在，每月亏损1000万元左右。其原因：一是库存生铁是采用加权平均法计算销售成本的，承担了前期部分高成本；二是生产不正常，产量低，固定费用负担较重；三是生铁价格和原燃料都是执行市场价格，林钢生铁高价位的优势基本不存在了。<br />
根据目前情况，如果开1号高炉，每月生铁产量1万吨，按目前原燃料价格测算，生铁销售成本（不承担以前高成本生铁和原燃料部分），每月亏损376万元左右。<br />
如果按全部停产测算，每月要发生固定费用640万元。主要是：职工工资320万元；上缴养老金、医疗保险、失业保险、工伤保险共96万元；工会经费、教育经费13万元；财务费用80万元，折旧费52万元，管理性费用支出30万元，必要的电费、水费90万元；从管理费用列支的各项税金11万元，不可预见费用 15万元。这就是不生产也要亏损707万元，比生产多亏334万元。<br />
如果开3号炉，按每月产量2.8－2.9万t，可使成本降低。经测算，盈亏基本持平，可资金和市场的影响也是非常现实的问题。从各方面综合考虑，近日内必须尽快恢复高炉生产。唯有这样，才能巩固市场，逐步挽回损失，保持生产经营良性循环，保持林钢继续生存和发展，这是确保职工利益最基本的前提条件。<br />
稳定是各项工作的关键。林钢建厂40年来，倾注了几代人的心血和汗水，以无私奉献和勤恳的工作铸就了现代的林钢，林钢也哺育了成千上万人的成长和幸福，林钢走到今天是来之不易的，我们要呵护它，保护它，使其更加健康地发展，尽管在改革发展中出现了一些矛盾和问题，通过大家的相互理解和公司的努力，会把问题妥善解决好的。目前林钢的现状是非常严峻的，只有恢复正常的生产经营和工作秩序，才能最大限度地保证广大干部职工的切身利益，否则将会使企业拖向可怕的境地，后果是相当严重的，要面对现实，以解决职工和职工家属基本的就业和生活问题为前提，恢复生产，保持稳定，使林钢尽快走出困境。<br />
各单位党政工团组织要充分发挥作用，公司领导班子成员及中层领导要深入职工中去，全力做好思想政治工作和治安保卫工作。广大干部职工要站在林钢生死存亡的全局高度，深刻反思我们面临的严峻形势，化解矛盾，减少内讧，形成共识，才能把企业稳定的各项工作做好。</div>

							
							
							
							
