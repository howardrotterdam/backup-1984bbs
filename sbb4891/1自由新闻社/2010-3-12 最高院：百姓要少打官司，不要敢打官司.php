核心提示：最高人民法院负责人张军回答网友提问时称，普通老百姓生活中发生一些矛盾很正常的,能不打官司尽量不要打官司,更不要敢于打官司。打官司,既劳民伤财,又伤感情。张军称，希望老百姓能不打官司都不要打官司,法院的案件越来越少是我们社会越来越和谐的一个表征。<br />
<br />
新华网3月12日报道 3月11日晚8时,邀请出席十一届全国人大三次会议的2位全国人大代表和最高人民法院1位负责人,结合听取和审议最高人民法院工作报告,就司法保障民生问题与网民在线交流。<br />
<br />
[中国广播网记者]中国广播网网友想请问张军代表,我无意中听说法院在面向残疾人、五保户等弱势群体时,在诉讼费等环节有减免、缓交等各种帮助措施,可是在平时的生活中,很少听说这方面的消息。今后法院能否在这方面加大宣传力度,让普通老百姓、弱势群体,也敢于打官司?谢谢。<br />
<br />
[张军]我们这位网友很有意思。“无意中听说”,说明这方面的宣传比起以前还是普及了。确实,人民法院在体现人民性方面有许多实实在在的举措,今天下午王胜俊院长的工作报告当中就特别讲到,我们法院在司法救助、为弱势群体提供更有力的司法保障方面做了许多实实在在的工作,去年一年,为因为生活困难交不起诉讼费的弱势群体减、免、缓交诉讼费一共是7.6亿,这个数字一年比一年要大,这体现了党和政府对人民群众的关怀。这一方面的宣传,我们今后会通过网络包括在座的各个网站向广大的网友进行宣传,也希望网友们能够帮助我们做好这方面的宣传,让更多的人“无意当中”听到。<br />
<br />
另外,我还想讲另外一个方面的问题。普通老百姓、邻里家庭生活当中发生一些矛盾是很正常的,能不打官司尽量不要打官司,更加不要敢于打官司,也就是说,能够通过人民调解、通过德高望重的长者替你主张权利,替你说话,把问题解决了,就不要打官司。打官司,既劳民伤财,又伤感情。我们中国的古话就是以和为贵,一打官司就伤了和气。所以,我们在诉讼当中能够通过调解解决的邻里、家庭纠纷,会尽最大的努力去调解,目的就是想让当事人今后能够有一个更和谐的工作、生活、学习环境。如果硬判下来,对方气没有消,不自动履行应该履行的赔偿义务,法院再强制执行,恐怕当事人之间一生都会生分,弄不好还会激化矛盾。<br />
