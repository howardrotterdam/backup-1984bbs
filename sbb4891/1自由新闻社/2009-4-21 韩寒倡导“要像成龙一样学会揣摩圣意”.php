													<h2>韩寒倡导“要像成龙一样学会揣摩圣意”</h2>
						
						
																											<div id="postmessage_78572" class="t_msgfont">韩寒<br />
<br />
&nbsp; &nbsp; 成龙说，“有自由好还是没有自由好……真的我们现在已经混乱了、太自由了，就变成像香港现在这个样子很乱，而且变成台湾这个样子也很乱，我慢慢觉得，我们中国人是需要管的。”<br />
<br />
&nbsp; &nbsp; 我觉得成龙这句话看似简单随口，但有着其逻辑上的不可反驳性，也是我写文章这么久第一次遇见这种情况。<br />
<br />
&nbsp; &nbsp; 首先，我不能说，中国人不需要管理。对于政府和人民的关系，我们通常有两种认识，一种就是成龙说的“管理“，一种就是一小撮人说的“服务”，政府自然愿意做老板而不愿意做服务员，因为服务员只能按照行业惯例来收费，而老板可以自定义收费，服务员只能在法律规定的范围内行事，而老板可以制定法律。管理和服务有什么区别呢？区别就是你到底是能成龙还是成不法分子。<br />
<br />
&nbsp; &nbsp; 我认为，在这个太自由的地方，我如果认为中国人不需要管，我就会出问题，所以我只能赞同成龙的观点，并且认为应该严加管理，从文化上来讲，我们应该重新开始避讳，比如说，领导人的名字是不能出现的，必须找个其他字代替，比较可喜的是，在很多论坛里我们已经实施了这种先进的管理制度。<br />
<br />
&nbsp; &nbsp; 所以，在这点上，说成龙胡说的人，都得抓起来，罪名有两个，一个是成龙思想是和上次两会领导的最后讲话其实是高度统一的，另外一个是，你没有避讳。<br />
<br />
 <br />
<br />
&nbsp; &nbsp;其次，成龙说，台湾现在这个样子很乱。<br />
<br />
&nbsp; &nbsp;首先，我不能说，台湾现在的XXXX很好，因为我们要会揣摩圣意，《环球时报》是一份揣摩圣意的非常好的报纸，所以，当说起台湾的华人民主时，有些学生会说，哈哈哈哈，这算什么民主啊，互相骂来骂去的还打架，就是个笑话。<br />
<br />
&nbsp; &nbsp; 你别以为这些学生在讲笑话，他们代表的态度就是圣意，成龙也属于揣摩的非常成功的，虽然在智商上，他们失败了，但是在政治上，他们成功了。<br />
<br />
&nbsp; &nbsp; 所以，在这点上，成龙又说对了，台湾现在这个样子很乱，这十个字，是上头一直想说但是憋着不好意思说的话，如果成龙能再接再厉，继续说出“朝鲜现在这个样子很好”“金正日的世袭制度符合朝鲜人民的利益”等话，那就是彻底揣摩到家了。对不起，家是不能说的，忘记避讳了。<br />
<br />
 <br />
<br />
&nbsp; &nbsp; 成龙又说，香港现在这个样子很乱。这句话貌似不对啊，一直揣摩的那么准的成龙为什么犯错误了呢，香港已经收回了啊，应该很好才对啊。这普通人就不懂了，这属于深度揣摩。香港在1997收回了没错，但因为英国的残酷殖民和文化封锁，两个凡是，三个代表，七不规范等先进思想并没有影响到香港，而因为文化差异巨大，所以我们实行了一国两制，两制就是两个制度，必须要分出好坏来，成龙的意思是说，香港也不够好，太自由，大家都在虎说八道（避讳），是XXXX的遗毒，如果香港能够和大陆使用一样的制度，那么明天会更好。成龙意在向政府提供决心，香港你们该管一管了。<br />
<br />
 <br />
<br />
&nbsp; &nbsp; 成龙一直以大哥的形象示人，也象征着他很喜欢管理，从成龙思想和他出席的一些活动，也可以看出成龙其实对大陆的政坛是有一定的追求的，而且要比艺术追求更大，这就是为什么成龙在新电影刚被大陆禁了还能说出我们过度自由这样的话，这是何等的忍辱负重。从他揣摩功力来讲，我觉得他有这个能力，但是很遗憾，我并不认为成龙能够做到中宣部部长文化部部长等职务，揣摩的再好也只能到文化部仁义大哥的级别。<br />
<br />
&nbsp; &nbsp; 为什么呢，因为成龙的名字起的不好，作为艺人，这是个好名字，作为官人，这会影响了他的仕途。成龙这个名字在封建社会，是反动的，在现代社会，又是封建的，这真是个头大的问题，当今圣上也不会允许身边有一个高官的名字叫成龙的，这也太虎视眈眈了，所以至于成龙，他继续拍好看的电影，他的很多电影我也很喜欢看，这就完全可以了。<br />
<br />
 <br />
<br />
&nbsp; &nbsp; 至于有些网友认为，中国人的确需要管理，要不然真的会很乱，我认为这是在偷换概念。任何国家任何星球的人都需要管理，但管理他们的，不是思想，不是制度，不是文化，不是宗教，不是意识形态，不是上级部门，而是合理的法律和尽量的公正。不需要管理不是说让你随便杀人放火，看中一个女人就上去强奸，而是说，当一个极其有权有势的人烧了你的房子杀了你的亲人强奸了你的女人的时候，你可以让他得到应得的下场，而不是在上访的路上被相关部门管理了，并且把你说成是精神病，你找媒体曝光，结果新闻得到了管制，消息全部被封锁，然后你被官方描述成一个虐妻的妄想症，后来在看守所跳橡皮筋摔死了，最后还把你当成丧心病狂的典范写进历史书里。</div>

							
							
							
							
