上虞市少先队城管学校成立仪式上,小城管队员在宣誓。图片来源:上虞市华维文澜小学校园网<br />
<br />
<br />
　红网长沙11月27日讯 (潇湘晨报滚动新闻记者 耿红仁) 旧闻成新闻，只因情节太雷人！去年6月的上海女子扔燃烧瓶抵抗强拆铲车事件被央视《经济半小时》11月21日报道后，引发了人们关于物权法和房屋拆迁条例的大讨论，而今又有旧闻成新闻，两年前浙江上虞市城管局和华维文澜小学共同创建的一个“上虞市少先队城管学校”，将小学生穿上清一色城管制服上街执法照片被网友翻出来引起不小的争议。<br />
<br />
<br />
　　网帖曝上虞市有所少先队城管学校<br />
<br />
<br />
　　“像廉政从娃娃抓起一样，难道城管也要从娃娃抓起？”11月26日，一则题为《上虞市居然有个少先队城管学校》的帖子在红网论坛、天涯社区及爱卡社区等网上热传，称两年前浙江上虞市成立了一间“少先队城管学校”，小学生穿着统一的城管制服，煞有介事得宣誓“我们是小小城管员，身着威严的制服，胸怀城管的热情，走上街头……”<br />
<br />
<br />
　　这篇帖子图文并茂地描述了“少先队城管学校”成立仪式的场面，称在2007年12月24日上午，“上虞市少先队城管学校”成立仪式在上虞市华维文澜小学举行，上虞市副市长郑建庆出席仪式并亲手将“上虞市少先队城管学校”的牌匾授予上虞市城管局黄建华局长，将“上虞市少先队城管学校综合实践基地”的牌匾授予华维文澜小学李正火校长。同时，成立了组织机构――“上虞市少年城管学校”委员会，邀请了郑市长作为名誉校长。<br />
<br />
<br />
　　创建该学校的目的是“城市管理从小抓起”<br />
<br />
<br />
　　为何成立这样一所学校呢？帖子提到了郑副市长发表的“重要讲话”：为了提高少先队员的城市管理意识，扩大城市管理的宣传教育阵地，根据“城市管理从小抓起”的理念，通过“少先队城管学校”这一载体，使学校成为教育的平台、宣传的载体、执法的阵地，增强城管执法的社会效应、城管宣传的辐射效应。<br />
<br />
<br />
　　而记者通过网络搜索发现，帖子所引用的文字和照片则是分别来自上虞市城市管理行政执法局与上虞市华维文澜小学的官方网站，其中也提到了创建该学校的目的：为进一步推进未成年人思想道德建设工作，培育学生的城市管理意识、文明意识和社会功德意识，以“小手拉大手”带动更多的学生和市民加入城市管理行列，唤起社会各界对城管工作的关注和支持。<br />
<br />
<br />
　　其中，李正火校长还代表“综合实践基地学校”作了表态发言：我校办学以来，一切从孩子的实际出发，尊重孩子的天性，创造性地开展了形式多样“无墙课堂”活动，走向社会，面向大众，贴近生活，孩子们走向人文热土玉水河畔的城北新区，拥抱英台之乡的文化广场，感受市府大院、市民大道“城北之光”，阅读新华书店的“好大一堂课”等，充分感受城市建设的巨大变化。<br />
<br />
<br />
　　成立仪式上小城管宣誓表演学生穿着单薄<br />
<br />
<br />
　　这篇“新闻”所提到的成立仪式上并不只是领导授牌和发言，还有就是“上虞市华维文澜小学36名学生成为了‘上虞市少先队城管学校’首批小小城管员。他们身着崭新的制服，英姿飒爽，精神昂扬。他们在大会上庄严宣誓：我是小小城管员，将以实际行动为城市管理贡献自己的力量！”<br />
<br />
<br />
　　而从“上虞市少先队城管学校成立仪式剪影”的照片上可以发现，还有数名穿着裙子、光着膀子的小学生正在台上跳舞，而颁奖仪式上的各位领导则都穿着冬装，好事网友发现了这一点，并在网上贴出了上虞市2007年12月24日的气象情况：最低温度7到8度，最高温度10到11度，并认为这样做有虐待孩子的嫌疑。<br />
<br />
<br />
　　除了这些，“新闻”还谈到了“成立仪式之后”的情况，“小小城管员们又来到了百官解放路步行街，和在步行街上进行城管执法的叔叔阿姨们一起工作，劝阻行人的一些不文明行为，共同为城市建设、环境美化努力。”<br />
<br />
<br />
　　从照片上看，几十名小学生穿着配有“少年城管”贴标的城管制服，扛着旗子，跟着城管人员走在被淋湿的街头“一起执法”。<br />
<br />
<br />
　　网友质疑：城管从娃娃抓起实则是作秀<br />
<br />
<br />
　　“这样搞显得太滑稽了，这本不应属于他们的应该肩负起的责任！”在网上各大论坛，有网友跟帖说，“这就像前不久杭州紫阳小学，在入学仪式上让新生都穿上清朝官服诵经，美其名曰是宣扬国学，实则是哗众取宠，还有再再以前的‘反腐败从娃娃抓起，廉政文化进校园’，在这个过程中，孩子成了大人掩饰问题、转移视线的工具，而且还打着堂而皇之的高尚理由，这种作秀令人恶心，救救孩子吧！”<br />
<br />
<br />
　　当然，也有网友认为，这之所以成为新闻是因为关于城管的负面新闻太多造成的，事实上这只是作为教育孩子的手段，未尝不可，而且少年城管学校在湖南长沙、山东济南、江苏昆山等地都有，“小孩的成长环境是每一个大人都有责任创造的，但愿我们能用更多的时间去教会我们的下一代怎样去学会理解别人，学会独立的生活，而不是碰见什么骂什么，怨什么，中国的孩子最需要学会的是独立和关爱，遍地戾气世界里怨恨和自私随处都能学到，如果我们不能教会他们他们最需要的，我们才是最应该被谴责的。”<br />
<br />
<br />
　　上虞市城管工作人员称学校还在 活动少了<br />
<br />
<br />
　　由于是两年前成立的学校，是不是个临时性学校，现在是否还有活动？11月26日下午，记者致电上虞市城管局，一金姓工作人员称网上描述的内容属实，学校还在，只是活动搞的少了，详情他也不清楚，让记者等会再打电话可以找他们办公室主任联系，于是，记者在他说的下班前致电过去，一位女性工作人员称主任已经开完会下班了，详情也不愿透露，而上虞市华维文澜小学的三部电话均一直无人接听。<br />
<br />
<br />
　　专家称体验可以但不能直接参与执法 穿城管制服不妥<br />
<br />
<br />
　　12月26日晚上，记者电话采访了中共湖南省委党校、湖南行政学院研究中心主任兼公共管理研究所所长、教授王学杰，王教授认为让小学生跟着城管到街头进行体验，而不是直接参与执法，增强法制意识未尝不可，但是，一定也要注意方式，比如，不能给小学生施加很强的责任，毕竟他们还小，不能将本属于大人的责任范畴强加到小学生的头上，不能耽误正常的学习时间。而且他也提醒说，在进行体验的时候，也没有必要穿上城管制服，否则显得有点不伦不类，事实上只穿学生服装戴红领巾就足够了。<br />
<br />
<br />
　　城管是咋炼成的，一般都是念啥经？<br />
<br />
<br />
　　11月13日，河南省出台关于文明执法若干规定的征求意见稿。根据意见稿，执法人员严禁采取简单粗暴、强取强夺、辱骂殴打等行为。在对方情绪激动或者有过激言行时，行政执法人员应当冷静处理，加以劝导，不得以违法对违法，激化矛盾；在受到轻微的攻击或推搡时，应当予以警告，要求其停止行为，但不能进行回击。<br />
<br />
<br />
