<br />
　　昨天晚上8点半，记者来到段晓宇的宿舍1201室，寝室有人但拒绝开门。随后，记者找到了赵秋瑞所在寝室一号楼1103室，但无人应门。同班同学一听到“赵秋瑞”的名字就迅速把门关上。据其他班级同学介绍，工业设计系0801班都已经知道赵秋瑞遇害一事，所以对外不愿多说。<br />
<br />
