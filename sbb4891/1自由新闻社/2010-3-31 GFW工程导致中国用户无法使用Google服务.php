谷歌（Google）表示，该公司搜寻服务今天几近瘫痪是中国改变审查作法所导致，而非如其先前所宣称，是谷歌本身的技术性瑕疵引发。<br />
 <br />
在今天稍早传出网路搜寻受到广泛干扰后，谷歌也表示，其在中国的搜索流量似乎已恢复正常。<br />
 <br />
谷歌今天针对其在中国的搜寻服务最近受到严重干扰提出评论，三个小时后又做出新的解释。<br />
 <br />
谷歌在最新声明中说，该公司改变搜索代码是一周前的事，而非在过去24小时。谷歌最先将改变搜索代码列为其在中国搜寻服务受到干扰的原因。<br />
 <br />
谷歌在一份电子邮件声明表示：“因此今天Google.com.hk网站遭到封锁，一定是‘防火长城’改变的结果。”它指的是中国过滤网路内容的科技。<br />
 <br />
谷歌在另外一份声明中也表示，该公司在中国的行动服务28和29日遭到部份封锁。<br />
 <br />
