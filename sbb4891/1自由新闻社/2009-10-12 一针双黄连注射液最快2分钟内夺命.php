													<h2>一针双黄连注射液最快2分钟内夺命</h2>
						
						
																											<div id="postmessage_278802" class="t_msgfont">2009年10月12日 来源：中央电视台 经济半小时<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;[提要] 双黄连注射液是一种常用的中成药，但9月安徽、云南和江苏三省却先后有三名患者死于注射双黄连注射液后的严重不良反应。据悉，产生过敏的主要成分是氯盐酸，氯盐酸是个过敏源，所以双黄连可能是一个有缺陷的产品…<br />
&nbsp; &nbsp;&nbsp;&nbsp;双黄连注射液引发不良反应的事件这几年一直在接连发生，明明是药品怎么会变身成杀手？中药注射液高疗效高风险，谁在拿患者的生命做实验？<br />
&nbsp; &nbsp;&nbsp;&nbsp;中国中医药科学院研究员、原国家药监局审评中心中药室主任叶祖光告诉记者，在医学界，中药注射液容易引发过敏反应早已不是秘密，尽管目前对于中药注射液的审批标准已非常严格，但现在市场上的100多种中药注射液有80%是老药，执行的是1985年之前的质量控制标准，而双黄连注射液等一些药剂，虽是新药，执行的也是15年前的老标准。<br />
&nbsp; &nbsp;&nbsp;&nbsp;湖南会同县药监局主任科员朱宝利发现，多次双黄连注射液引发的死亡事件，并非是药物本身有质量问题，而是患者对药品产生了严重的过敏反应，如果严格按照国家药监局的四点要求去做，在临床上发生过敏反应不良的状况就会大幅减少，但是他发现这四条措施实施起来难度很大。<br />
<br />
<br />
&nbsp; &nbsp; 追查夺命双黄连<br />
<br />
　　（记者：张子贤、郭莹莹 摄像：沈焱、李培、张明）<br />
<br />
　　双黄连注射液是一种常用的中成药，有个感冒发烧、头疼脑热，打上几针，比很多抗生素便宜多了。而且还有很多人认为既然是中药制剂，它的副作用比抗生素要小，应该更安全。<br />
<br />
　　可是，就在上个月，安徽、云南和江苏三省，却先后有三名患者，注射了黑龙江多多药业有限公司生产的双黄连注射液之后，死于严重不良反应。<br />
<br />
　　为确保公众用药安全，国家食品药品监管局决定暂停销售和使用标示为多多药业有限公司生产的双黄连注射液。为什么普普通通的双黄连竟然引起如此严重后果？质监部门正在追查，我们栏目的记者丁高波也赶到了事发地江苏泰州。<br />
<br />
　　双黄连注射液一针夺人命<br />
<br />
　　9月14号，泰州市居民刘春梅就是在这间诊所，注射了多多牌双黄连注射液后，发生严重不良反应，经抢救无效死亡。<br />
<br />
　　9月18号，当我们的记者赶到江苏省泰州市原野小区时，刘春梅的家人仍然沉浸在失去亲人的的悲痛中。<br />
<br />
　　附近小区居民：“才32岁啊，上又老下有小的，很可惜，对这个家庭损失很大啊。”<br />
<br />
　　这里就是刘春梅出事当天就诊的文诚社区诊所。几经周折，记者找到了当天的接诊医生王水庚。<br />
<br />
　　泰州市文诚诊所医生王水庚：“心里不好受，真的不好受。”<br />
<br />
　　这家社区诊所距离刘春梅家不到1公里。王水庚清楚记得，刘春梅是9月14号6点50分左右来的诊所。<br />
<br />
　　王水庚：“她说我发热两天，原来喉咙还有点疼。”<br />
<br />
　　在询问完刘春梅的基本病征，并进行体温检测等常规检查之后，王水庚诊断刘春梅患的是上呼吸道感染，并开具了处方。随后，护士为刘春梅挂上了点滴。但挂上第二瓶药水不久，刘春梅跟王水庚说感觉全身很不舒服。<br />
<br />
　　王水庚：“她开始讲发抖，怕冷到发抖，我就首先我就首先考虑的是过敏反应了。”<br />
<br />
　　王水庚赶紧停止输液，立即为刘春梅静脉推注了两针抗过敏药物，并让刘春梅电话通知她的家人。在刘春梅的丈夫到达诊所后，刘春梅被送到了泰州市中医医院急救。但不幸的是，9月15号上午9点半，泰州市中医医院宣布，刘春梅因过敏性休克死亡。<br />
<br />
　　王水庚：“我已经尽心尽力了，她这个事情我是无能为力了，我已经竭尽全力了。”<br />
<br />
　　只是普通感冒发烧，用的药物也并没有什么特别之处，到底是什么导致了刘春梅的死亡呢？意外发生后，泰州市卫生局、药监局以及公安部门立刻对此事展开了调查。并当即查封了文诚诊所，将当晚给刘春梅使用的药物进行了提样送检，并对药物购进渠道进行调查。<br />
<br />
　　泰州市食品药品监督管理局副局长、副主任药师刘露：“就是这个诊所应当说还是有许可证的，卫生部门发的许可证，那么我们查的所使用的药品购进的渠道，也没有什么，应当也是正规渠道。”<br />
<br />
　　诊所合法、药品渠道也是正规的，那么，到底是什么原因导致刘春梅因过敏性休克死亡呢？据我们了解，今年66岁的王水庚拥有合法的医师资格证书，并且有着几十年从医经验，对于这次意外的原因，王水庚至今也百思不得其解。<br />
<br />
　　王水庚：“一直是这么操作的，没有遇到过问题，我问过她的病史了，她没有什么特殊疾病，也没有什么药过敏史。”<br />
<br />
　　王水庚回忆，当时他给刘春梅开的是很常规的处方。两瓶点滴中第一瓶是头孢他叮、病毒唑和地塞米松的盐水溶剂，第二瓶则是双黄连注射溶剂。<br />
<br />
　　王水庚：“我怀疑药的问题，双黄连反应。”<br />
<br />
　　记者：“为什么？”<br />
<br />
　　王水庚：“第一瓶很正常，双黄连是第二瓶接的，第一瓶很正常。”<br />
<br />
　　我们就王水庚所描述的治疗程序及所开药方也向有关医学专家进行了咨询，他们也没有发现有违反常规的地方。我们还了解到，就在泰州之前， 9月7日和12日，安徽、云南也有两名患者在注射双黄连注射液后出现死亡的情况。和泰州的刘春梅一样，他们注射的药品也是来自黑龙江多多药业有限公司。我们的另一路记者张自显也赶到了多多药业的所在地黑龙江佳木斯。<br />
<br />
　　记者：“9月16日，国家卫生部和国家食品药品监督管理局下发了立即暂停销售和使用黑龙江多多药业生产的双黄连注射液的通知之后，我们来到了位于佳木斯市的黑龙江多多药业，据我了解，这家企业每年生产的双黄连注射液超过两亿支，而目前全国每年的生产量只不过六亿支，目前这家企业的生产怎么样了呢，让我们进去看一看。”<br />
<br />
　　记者：“我想问一下，这里是液体制剂车间？”<br />
<br />
　　黑龙江多多药业公司工作人员：“恩。”<br />
<br />
　　记者：“这个车间还在生产吗？”<br />
<br />
　　黑龙江多多药业公司工作人员：“没生产，没生产了。”<br />
<br />
　　记者：“什么时候开始停的？”<br />
<br />
　　黑龙江多多药业公司工作人员：“十六号，十六号就停了。”<br />
<br />
　　记者：“之前这个车间是生产什么的？”<br />
<br />
　　黑龙江多多药业公司工作人员：“双黄连（注射液）。”<br />
<br />
　　在多多集团办公室，副总经理李建告诉记者，黑龙江多多药业有限公司是一家有着1500多名员工的股份制医药企业，先后通过了国家食品药品监督管理局多个剂型和车间的GMP认证，双黄连注射液是这家企业的支柱产品，而多多药业目前也是国内十余家双黄连注射液生产企业中的年产量最大的一家。<br />
<br />
　　记者：“你们这个产品时什么时候获得的国家的批号，什么时候开始生产的？”<br />
<br />
　　黑龙江多多集团 副总经理李建：“1995年，到现在已经十四年了。”<br />
<br />
　　记者：“你认为这一次的不良反应事件是什么原因造成的？”<br />
<br />
　　李建：“我们初步所了解到的情况看是不谨慎用药或者是混合用药造成的。”<br />
<br />
　　记者：“那你认为你们的产品是没有问题的吗？”<br />
<br />
　　李建：“我个人认为，我们企业出厂的产品，肯定是合格的，不合格的产品时不允许出厂的。”<br />
<br />
　　记者：“那之前有没有发生过多多生产的双黄连注射液的不良反应事件的报告？”<br />
<br />
　　李建：“像这一次疑似不良事件的发生还是头一次。”<br />
<br />
　　记者：“但是据我了解，在今年的四月份，重庆有个叫肖桂华的人，他的父亲因为注射了你们多多的双黄连注射液死亡，这个事情你不知道吗？”<br />
<br />
　　李建：“不知道，我这还真没听说这个事情。”<br />
<br />
　　记者：“据我了解，北京市药监局在今年4月份第一季度的药品抽查公报中，其中有一项，是你们企业生产的双黄连注射液的某一个批次的产品，发现了澄明度的问题，而且，他们对使用这个药品的诊所进行了处罚。”<br />
<br />
　　李建：“你北京是这么认为的，别的省不一定是这么认为，这一项不行就全不行，你全检嘛，你这一项不行就所有都不行吗，不是这个道理。”<br />
<br />
　　记者：“那这个责任最后到底在谁？”<br />
<br />
　　李建：“现在讲，查清事件责任之后，该是谁的，是谁的。”<br />
<br />
　　在佳木斯食品药品监督管理局，新闻发言人李新成告诉记者，对于多多药业这样生产中药制剂中高风险药品的企业，药监部门对他们的生产监管非常严格，不仅在厂内派驻了驻厂监督员，同时还对生产车间不定期进行专项检查。<br />
<br />
　　记者：“你们平时对多多药业的监管过程中，有没有发现或者接到他们的产品有不良反应的报告。”<br />
<br />
　　黑龙江佳木斯食品药品监管局新闻发言人李新成：“到目前为止，一直没有发现这家企业的双黄连注射液发生过类似这样的不良反应。”<br />
<br />
　　记者：“那么这次事件你认为可能会是什么原因？”<br />
<br />
　　李新成：“针对这次不良反应事件，国家食品药品监督管理局已经派了专家组进驻企业，进行深入细致的调查，我们省市药监局成立联合调查组，全力配合国家局的工作，至于此次事件，原因目前尚不明朗，还在进一步调查之中。”<br />
<br />
救命药为何变成“夺命药”？<br />
<br />
　　多多药业和佳木斯药监局都表示在双黄连注射液的生产环节，目前为止还没发现过问题。不过，双黄连注射液引发不良反应的事件这几年却一直在接连发生。<br />
<br />
　　早在2001年11月，国家药品不良反应监测中心就首次通报了双黄连注射剂引起的不良反应。通报发布后，由于注射双黄连注射液而引发患者不良反应，甚至死亡事件屡屡发生。今年2月7日，山东省邹城市一男子，因为感冒在卫生所注射青霉素和双黄连注射液，出现过敏反应后死亡。今年2月11日，青海省大通县3名患者使用双黄连注射液发生不良事件，死亡1例。2月16日，山东省食品药品监督管理局又通报，自2008年以来，山东省药品不良反应监测中心共接到56例双黄连注射液引发的不良反应病例报告。而且这些事件使用的注射液还涉及到其他厂家的产品。<br />
<br />
　　听说这么多案例，我们每个人都会追问，明明是药品怎么会变身成杀手？双黄连到底怎么啦？一位药监人员为我们揭开了谜题的另一面。<br />
<br />
　　记者：“在你看来这是一个偶然事件吗？”<br />
<br />
　　湖南会同县药监局主任科员朱宝利：“这不是一个偶然事件，这是一个脓包，它总要捅破的。”<br />
<br />
　　朱宝利，湖南会同县药监局主任科员，常年负责药品不良反映监测工作，4年前一个偶然的机会，他开始关注双黄连注射液引起的不良反应。<br />
<br />
　　朱宝利：“原来我一个同学，他原来在市里医药研究所，他连续碰到了几次就是双黄连引起的严重过敏反应，他就跟我讲，我在怀化开会，跟我讲，讲双黄连很危险，效果很好，但用起来很危险，我就想双黄连那么危险，那么危险还老在用，特别在我们基层用量比较大，那个时候我就收集有关资料的。”<br />
<br />
　　朱宝利把会同县从2005年到2008年，因注射双黄连产生不良反应的病例集中进行了调查，结果让他非常震惊。<br />
<br />
　　朱宝利：“128起，4年以内，在县一级的医院我们发生的不良反应发生率这是有十几起，一级严重的有两起，然后在乡镇这一级，乡镇这一级别大概有40多起，40多起严重的大概有十几起，好，在卫生室这一带和个体诊所这一带大约有60多起，70来起，三分之一是严重的休克的，有四起疑似使用了黄双黄连的死亡事件。”<br />
<br />
　　朱宝利发现，这128位病人，在注射双黄连注射液后，都有过相似的反应。<br />
<br />
　　朱宝利：“最典型的就是我们讲的血压降低了，用了双黄连以后浑身在发抖，不及时采取抗过敏措施，他马上就休克了，有的人就是什么，就是发烧，原来我们过敏反应，在我们教科书上都没有提到发烧是过敏反应这一种，但那我经常看到有人发烧，突然发起高烧来了，高烧不用抗过敏药，他就会转为严重的过敏反应。”<br />
<br />
　　记者：“能有多块，一旦注射？”<br />
<br />
　　朱宝利：“有的只有5分钟，快的就是2、3分钟，慢的只有7、8分钟。”<br />
<br />
　　记者：“致人死亡速度的会有多快？”<br />
<br />
　　朱宝利：“致人死亡它只要发生了严重，就是严重休克的话，只有几分钟就会死亡。”<br />
<br />
　　病人的严重不良反应到底因何而起呢？在查阅了大量资料以后，朱宝利作出了判断。<br />
<br />
　　朱宝利：“主要一个药物他也就是金银花，它的主要成分也就是氯盐酸，它是氯盐酸又是个过敏源。”<br />
<br />
　　记者：“你基本上做出一个判断？什么判断？”<br />
<br />
　　朱宝利：“双黄连可能是一个有缺陷的产品，很可能就是说经常我们发现的过敏反应就是它的药品本身它的过敏源所导致的，过敏性休克引起的死亡事件。”<br />
<br />
　　朱宝利发现，多次双黄连注射液引发的死亡事件，并非是药物本身有质量问题，而是患者对药品产生了严重的过敏反应。<br />
<br />
　　朱宝利：“中药它一直是我们国家的是一个国粹，而且在临床上又证实了它有一定的疗效，很遗憾的就是它不良反应太多了一点，最好更进一步提高工艺，使中药注射剂的纯度高一点。”<br />
<br />
　　记者：“为什么双黄连作为口服液的时候，不会引起这种不良反应，但是注射到体内，就会出现这种严重的不良反应呢？”<br />
<br />
　　朱宝利：“过敏源因为人体的胃肠，它有一个屏障，有些过敏源不被吸收，被排出来，如果你静脉注射的时候，过敏原直接打入到血管内，这就非常危险了。”<br />
<br />
　　随着调查的深入，朱宝利发现双黄连注射液存在的安全隐患。<br />
<br />
　　但是我们能不能减少不良反应发生的概率呢？朱宝利多方调研之后，开始意识到双黄连注射液存在的问题不简单，于是，他开始给温家宝总理写信，给卫生部写信，他的信引起了药品检测部门的重视，今年5月18日，他收到了国家药监局的回信，随后国家药监局药品不良反应中心发布紧急信息通报，要求医疗单位警惕双黄连注射剂的严重不良反应。<br />
<br />
　　朱宝利：“重点就提到了双黄连，第一个就是讲非必须情况下，原则上不用双黄连禁滴，再一个就是讲建议尽量口服或者剂注，第三个就是用前要详细了解病人的过敏史，第四点就是讲，医疗机构急救设备不完善的情况下，尽量，它是建议慎重，主要就提了这四点。”<br />
<br />
　　朱宝利发现，如果严格按照国家药监局的四点要求去做的话，在临床上发生过敏反应不良的状况就会大幅减少。但是他发现这四条措施实施起来难度很大。<br />
<br />
　　朱宝利：“国家局发的通报，它只是建议慎重，我们讲只是判了个死缓，就是讲你的警惕性比较高的话，你尽量就不用了，可能有些人他胆子比较大还在用，第二个就是讲他讲的要询问药品的过敏史，病人的过敏史，这点有的时候很难做到，因为在我们正常的人群中，就是讲对很多物质过敏的比例特别高，特别我看到有关资料上他报道，就对食品的过敏率大概有30%到50%，所以有的人他自己都不知道自己是个过敏体质，中药它不是某一个单一成分，它是一个混合物，有的里面含有几十种，十几种，几十种的化学成分，所以化学成分，成分多了它就引起过敏。”<br />
<br />
　　把药品不良反应反馈给上级主管部门的同时，朱宝利多次联系生产厂家，希望对双黄连引起的不良反应，厂家能给予足够的重视。<br />
<br />
　　朱宝利：“在我们县里发生一起疑似用过双黄连以后死亡，我直接给黑龙江的多多药厂打过电话，他开始答应过来，但他要求我们不要把这个不良反应上报，以免影响他厂里的销售，我讲但是双黄连的氯盐酸是一种过敏物质你要注意，他怎么讲，这个我们清楚，讲今年的，哈尔滨专门讨论过这个问题，再一个双黄连死亡的事件又不是我们一个厂里有，其他厂里也有，正好国家局发了那个通报他就不肯过来，讲都是你捅出去的，我还过来干什么。”<br />
<br />
　　中药注射液高疗效高风险，谁在拿患者的生命做实验？<br />
<br />
　　朱宝利的担忧并不是空穴来风，国家食品药品监督管理局提供的数据显示，我国目前中药品种有九千多种到一万种，而列入国家标准的中药注射剂只有109种，只占中药总比例的1%。但是目前70%的中药不良反应案例都跟注射液有关系。<br />
<br />
　　怎么样才能避免中药注射剂的安全隐患，更好地拓展中药市场？今年7月，国家药监局已经正式启动再评价工作，将围绕保障药品安全这个核心，从风险排查、综合评价、提高标准三个层面展开，双黄连注射液被列为首批再评价品种。但中药注射剂由于它的特殊性，需要药品生产管理各方拿出更多的行动，才能消除安全隐患。我们也专访了相关专家。来听听他们的意见。<br />
<br />
　　叶祖光，中国中医药科学院研究员，原国家药监局审评中心中药室主任，长期从事药品不良反应研究。<br />
<br />
　　中国中医药科学院研究员、原国家药监局审评中心中药室主任叶祖光：“我收集的中药的不良反应。拿中药不良反应做一个分母，然后把中药注射剂的不良反应做一个分子，求出来的这个百分比是大约75%，如果我们再把中药注射剂的所有的不良反应再做一个分母，把过敏反应做一个分子的话，那过敏反应大约也是占到70%到80%。”<br />
<br />
　　叶祖光告诉记者，在医学界，中药注射液容易引发过敏反应早已经不是什么秘密。<br />
<br />
　　叶祖光：“中药注射剂最可怕的不良反应是过敏反应，我们不可能拿人做过敏试验，一做要死人的，而现在是从动物做试验做的所有的过敏，动物的实验来预测可能在人身上发生的这种预测的一致性，可预测性，非常差的，第二，又回到我们中药的多成份，我们在技术上不可能把每个成份都分出来，而且也分不出来，有的它的含量可能很小，你以为那含量小了就没作用了，过敏按照经典上的过敏反应它和剂量是无关的。”<br />
<br />
　　叶祖光告诉记者，除了技术障碍，中药注射液质量控制标准过低也是导致出现不良反应的重要原因。尽管目前对于中药注射液的审批标准已经非常严格，但是现在市场上的100多种中药注射液，有80%是老药，执行的是1985年之前的质量控制标准。而双黄连注射液等一些药剂，虽然是新药，执行的也是15年前的老标准。<br />
<br />
　　叶祖光：“你比如说现在我们报一个新药，报一个新药的话，你要做分离、提取，里面的有效成分我要含量是多少，第二，要做安全性试验，毒理试验，引起不引起毒性，可是在1985年以前就没有这么规范，没有这么严格，没有这么全面。”<br />
<br />
　　我们得知，按照老标准，中药注射液只要求对其中25%的成分进行有效控制，以双黄连注射液为例，仅有黄芩苷一项作为有效成分含量控制指标，对药品处方中金银花及连翘的有效成分则没有含量控制指标，而对树脂、砷盐等多项有害物质及总成分也未进行有效控制。<br />
<br />
　　叶祖光：“常常也就是定一个成分，两个成分，出场一测，这个成分百分之几，那个成分百分之多少，一测都是，都合格，但是这话说回来，它固有的问题没解决，因为它可能有几十个成分，你只不过取沧海一粟而代表之。<br />
<br />
　　张世臣，原国家药监局药品注册司司长，他告诉记者，中药注射液早期质量控制标准过低有历史原因。上世纪40年代，柴胡注射液作为首例中药注射液开始用于临床，到上世纪七十年代，中药注射液的品种一度达到了1000多种。<br />
<br />
　　中国中药协会副会长张世臣：“七十年代，那个时候大搞中草药运动，做了很多中草药注射液，这些个绝大多数没有做过安全（测试），就是它基础研究不够，有缺陷，重大缺陷。”<br />
<br />
　　缺医少药的特殊年代造就了中药注射液生产的大跃进，此后因使用中药注射液发生不良反应的事件频出。尤其是近几年，双黄连，茵栀黄、清开灵等中药注射液，多次引发严重不良反应，有的甚至危及患者生命。在医学界，中药注射液甚至成了高疗效高风险的代名词。1985年，我国实施《药品管理法》，对中药注射液的监管逐步加强，副作用过大，安全性难以得到保障的药品开始被逐步淘汰。<br />
<br />
　　张世臣：“九十年代的时候经过整顿了，就剩一百多种了，我给它改成是边整顿边提高，但是成立专家药品监督管理局之后，这个提高工作，没有继续做下去。”<br />
<br />
　　记者：“为什么？”<br />
<br />
　　张世臣：“那和某些领导人可能有些关系吧，我也不便评价。”<br />
<br />
　　针对这100多种中药注射液遗留的的安全性问题，2002年和2006年，国家药监局先后又进行了两次整顿，但两次行动都因为缺乏强制性措施而最终搁浅。<br />
<br />
　　叶祖光：“专门针对老品种，从它的适应症是不是合理，质量标准，就刚才你说，你过去欠的账你起码按照西药标准你得给我补上啊，不少企业就观望，你不逼到临头的时候我就是不做。”<br />
<br />
　　记者：“不是强制措施吗？”<br />
<br />
　　叶祖光：“不是。”<br />
<br />
　　诸多未知的高风险使中药注射液一直处于争论的风口浪尖。反对者认为中药本身的复杂性和生产过程中的不可控因素决定了中药不适于做为注射剂使用，甚至要求全面封杀中药注射液。而支持者则认为中药注射液作为我国传统医学和当代科学技术的有机结合，在一些急重病例的治疗中起着不可替代的作用。对于围绕中药注射液的这些争论，叶祖光告诉记者，其实西药注射液的副作用更高，但是在医学界，对于西药注射液的使用却几乎没有什么争论。<br />
<br />
　　叶祖光：“搞西药的时候有一个叫利弊平衡，我研发这个注射剂，给病人会带来多大的利益，有多大的风险，你们看看那抗癌药给病人折腾成什么样了，生与死的，你冒点风险值得，我们有些个尤其中药的老品种需要在利弊平衡上认真地在天平上平衡一下，就是说你到底值得不，我当时在药品审批中心我就有一个科研单位就是要治疗感冒，叫我给退审了，我说，让一个普通感冒，喝白水在家休息就能好的患者，冒着生与死的危险打你这中药注射剂，这个风险是不值得的。”<br />
<br />
　　现在多多药业生产的双环连注射液已经被国家药监局紧急叫停了，不过，记者调查中发现，目前市场上还有10多个品牌的双黄连在销售。看看它们现在销路怎么样？<br />
<br />
　　“请问一下，你们这的双黄连注射液卖的都是哪几种？”<br />
<br />
　　“多多的停止销售了，其他的这都有。”<br />
<br />
　　“都有货？”<br />
<br />
　　“要几百件都有，都有现货。”<br />
<br />
　　“都有？”<br />
<br />
　　“要开（票）就赶紧开（票），说不定马上明天还要涨价。”<br />
<br />
　　“明天还涨啊？”<br />
<br />
　　“说不定明天还涨呢，这谁也说不定，前几天多多的还能卖，现在多多的不能卖了，厂家越来越少，就引起涨价。”<br />
<br />
　　记者了解到，针对双环连等中药注射液的使用，在香港等地都有相关的明确规定。<br />
<br />
　　香港大学中医药学院博士冯奕斌：“目前还没有一个注射剂可以在香港使用。我曾经也在日本留学，据我了解也是没有。”<br />
<br />
　　记者：“有关中药适不适合注射，您有您的看法吗？”<br />
<br />
　　香港大学中医药学院博士冯奕斌：“你完全说中药注射剂不能用，从大原则来讲是，也是不对的，但严格来讲它安全性要求是非常高的，不可以轻易就获得审批通过。”<br />
<br />
　　稍微了解一点中药常识的人都知道，传统医学强调辩证治疗，表面上看是同样一种病，但中医会根据每个人不同的体质、病因开出不同的药方，而在中药生产工艺上，也有很多讲究，过去一些大的药堂对不同产地、不同质量、甚至不同时令的药材如何炮制使用都有严格细致的规定，稍有差池，最后的药效就可能千差万别。<br />
<br />
　　这和用化学制剂标准化流水线生产西药有天壤之别。正因为中医药的特殊性，我们不能把中药的现代化简单理解成用西药生产方式来制造中药，这种削足适履的结果，才是引发双黄连事件的根源。当有关部门对中药注射剂安全性再评估的时候，希望他们也能更深刻地反思这方面的教训。百年老店同仁堂的创始人曾给后人留下过一条古训，炮制虽繁必不敢省人工，品味虽贵必不敢减物力。中药可以现代化，可以从药汤变成注射液，但这条古训我们什么时候都不能忘记。</div>

							
							
							
							
