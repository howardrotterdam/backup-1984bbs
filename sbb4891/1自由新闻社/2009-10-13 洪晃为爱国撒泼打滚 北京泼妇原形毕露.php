													<h2>洪晃为爱国撒泼打滚 北京泼妇原形毕露</h2>
						
						
																											<div id="postmessage_279154" class="t_msgfont">组员@xiahua翻译，里面有些错字，凑合看吧<br />
<br />
[翻译试作] China’s class ceiling<br />
原文 <a href="http://www.latimes.com/news/opinion/commentary/la-oe-buruma11-2009oct11,0,8205.story" target="_blank">http://www.latimes.com/news/opin ... 9oct11,0,8205.story</a><br />
<br />
对于大多数人来说，中国主席胡锦涛是一个讨人嫌的人，包括13亿中国人。近来中国已经从一个贫穷的、鲜血染红的集权国家，变成了一个超级大国，雄心勃勃地意欲在不久的将来赶超美国。相比这一戏剧般的巨变，胡的呆滞无趣却是格外惹眼。但也许他缺乏魅力也不坏。共和国的前27年里，在毛主席的领导下，上百万人在无休止的清洗和运动中死亡，更有上千万人死于奇异的经济试验。也许这些失败使得中国人从此不吃“魅力领袖”这一套。<br />
人类历史上的古老文明，迄今仍有巨大影响的，只剩下了中国，并且中国人颇以之为傲。所以为什么要捣乱呢？也许让胡一样的技术专家来统治，把事情收拾得光鲜又瓷实，好得很呢。<br />
当然，要是去问东北那些穿着破腰带的失业工人们，去找找广东土地被强征的暴动农民，他们才不会这么说。同样，那些和腐败官员较量的勇敢律师们，以及主张中国人要有基本民主权利的持不同证件者们，也不会这么想。<br />
但是，对于新一代城市精英们来说，这是他们普遍的想法。他们受益于富裕时尚和快乐的流行浪潮，没有一个是共产主义理想者。都深深相信邓小平的指示：“致富光荣”。其中不少60后们，曾经参加天X门广场上的游行示威，要求过民主自由和结束腐败<br />
对这种矛盾，有一个小的突破也是很危险的，外国人尤甚。在北京新精英中有那么一个名人，一个成了中国致富荣耀代言人的事故女人，也“碰巧”是共产贵族的女儿。媒体大亨洪晃，她脸很圆，穿着昂贵，拥有一串流行杂志她妈妈是毛泽东的英语教师，她继父曾是毛的外交部长（指章士钊和乔冠华――译者注）。洪晃在纽约受过教育，她的丈夫之一――导演陈凯歌，也是北京镀金时代的有头有脸人物。<br />
几年前，我参观过洪晃可爱的乡间别墅。我是由共同的朋友――先锋派诗人杨炼介绍的。杨炼和她的小说家夫人友友住在伦敦。严格说来，杨炼和友友都不是政治上的持不同政见者。他们不总写政治，但他们都是有自由精神的作家，自己选择拒绝与这个威权社会合作。<br />
夜晚友好的降临，先是关于北京圈子里熟人们的闲话。然后洪晃开始给杨炼出主意：为什么还住在国外呢？为什么不回国呢？现在中国好极了。能赚大钱。杨炼你该跑跑项目。现代诗能骗骗老外，但北京的生活也在继续。她应该做广告，或者写写流行的抒情诗。只要游戏规则烂熟于心，根本不用担心审查不过。<br />
某种焦躁爬进了凉爽的山区空气。 洪晃的建议越来越像是在威吓。没人提六V四，但这无法绕开。因为杨炼和友友之所以选择住在海外，这是理由之一。突然间，洪晃提起来这茬，同时把头转向我：“天X门，天X门”她说“外国记者总是说天X门，我想该把它给忘记了。我们应该向前看，为我们的祖国感到骄傲。外国人只不过拿他来抹黑中国。”<br />
我感到必须得说点什么，但作为洪晃的客人，我可不想和她打架。所以我提出意见：中国人还执着地记着37年的南京大屠杀。当时日本军队在中国的首都奸淫作乐，烧杀掠劫。实际上，这件可怖的事件也是当下所谓“爱国主义教育”的一个核心内容。另一方面，日本右翼则希望日本青年忘记这件事。他们觉得“应该向前看，为我们的祖国感到骄傲”<br />
当然，我还是挑起了争斗。我永远也忘不了洪晃――一个有魅力的女人，一个世界主义者，纽约学成的人――摇身一变为咆哮的红卫兵。尖叫着辱骂我，辱骂所有外国人，还有为我辩护的杨炼和友友。很清楚，我刺痛了她的神经。<br />
是的，洪晃说的都是对的。人民，特别是有良好教育的，有点世界主义做派的，在后1989时代的中国做的都很正确。他们闷声发大财，一大笔钱。流行时尚急速发展，等等等等。但作为代价，就是洪晃所说的“游戏规则”――知道什么话题不该去碰，如何装饰你的观点，怎么远离政治。让呆板的技术官僚们戴着天鹅绒手套去治国吧――拒绝游戏规则的人，先给你们戴上镣铐。<br />
这种选择是完全可以理解的。流放是艰辛的，谁想进监狱呢？此外，对那些做出必要妥协的“富起来的人”来说，生活是很甜蜜的。但他们是妥协者。<br />
因为大多数外国记者，商人，外交官和教授们，更愿意和这些有良好教育的既得利益者们交谈（如：洪晃）。大多数有关中国报告反映了他们的观点：软威权主义对中国有好处。中国普罗大众对民主缺乏准备。给他们点投票权就会乱。这种技术专家政治论的主要论点是：这种体制更高效。不光是中国精英们，西方国家也渐渐能听到这种声音。一旦统治者决定专注于某件事－比如闹运会，计划生育，经济改革，可能甚至是控制污染－没人能阻挡注他们成功的脚步。<br />
那些欣赏强大的中央政府和严密管理组织的人，常常被中国模式吸引。还有那些更喜欢和威权政府打交道而不是工会的商人们。中国经常喜欢和印度比较：印度总体来说没效率，赤贫，还有巨大的问题：文盲，腐败和有组织犯罪。看起来，混乱的民主正在拖累印度。中国则在稳步前进，展示着更多惊人的统计数字。<br />
这种观点有一部分事实。当我在70年代时第一次看到深圳时，它还是一个香港边儿上的小农村。自从邓小平在82年宣称在那里建一个经济特区后，她的愿望迅速成为了现实。现在深圳是工业化大都市，人口大约有100万。<br />
然而，技术专家政治论也是有很大缺点的。威权主义的技术专家们在紧急状态下常常表现拙劣。08年四川省发生大地震，死亡约7万，导致一千万人无家可归。中国为它的快速和富有爱心的措施而备受称赞。但很少提到的是，数量很不相称的受害者都是死于校舍垮塌的儿童。建筑商们使用了劣质建材，官员们收了钱就不在过问。<br />
也许不应如此指责北京的技术专家政客们，但中央政府的作为也是被夸大了。大部分的援助都来自迅速赶往现场的普通百姓，而且最初当地政府官员们也是百般阻挠。后来，当公民和律师们师徒调查导致儿童死亡惨剧的腐败行为时，他们却被阻碍，甚至有人被投入监狱。<br />
专家政府的另一特有劣势，在于政治的核心问题：解决利益冲突。缺少政治自由的同时，个人自由有所增加。国家不再决定你的婚姻，你的住房，你的工作。但任何意图使个人脱离国家，形成组织的努力都会被无情的粉碎。老派马克思主义者叫这个“矛盾对立”。对上海商业精英有利的，对四川农民就未必。<br />
为确保对权力的垄断，中国的技术专家官僚们依赖着对永恒经济增长和秩序的许愿。因为法律的不完善，他们完全不能被惩罚。大错误也都没人检查。利益的腐烂或者是暴力中的腐败。中国的技术专家们也许看起来稳固，取得暂时成功，但在最基本的政治改革面前，难说能持续多久。有人期待哈佛耶鲁的毕业生们，那些新一波的技术专家们能解决这个问题。谁也不知道。但只要他们还没能解决，不论怎样我还是愿意把钱投给乱一点的民主。<br />
