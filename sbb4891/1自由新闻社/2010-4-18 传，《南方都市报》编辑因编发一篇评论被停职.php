													<h2>传，《南方都市报》编辑因编发一篇评论被停职</h2>
						
						
																											<div id="postmessage_569226" class="t_msgfont"><a href="http://news.boxun.com/news/gb/china/2010/04/201004171609.shtml" target="_blank">http://news.boxun.com/news/gb/china/2010/04/201004171609.shtml</a><br />
<br />
(博讯北京时间2010年4月17日 转载)<br />
&nbsp; &nbsp; （维权网信息员肖闻报道）本网获悉，4月17日《南方都市报》历史评论版编辑朱蒂因4月11日编发在该版的历史评论《爱国家不等于爱朝廷》而遭停职处理，由广东省委书记汪洋作出批示。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;朱蒂发给朋友的短信说：“南都上周历史评论版我编发的《爱国家不等于爱朝廷》终于事发。我被停职了，汪作批示，省里要求问责，我上黑名单了。” (博讯 boxun.com)<br />
<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 下面是这篇历史评论的具体内容：<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 洪振快：爱国家不等于爱朝廷<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 据说法国波旁王朝的君主路易十四说过“朕即国家”的话，尽管全世界的君主都喜欢专制，但很少有人会像路易十四那样露骨和无所顾忌。路易十四于 1643―1715年在位，同时代的中国皇帝是康熙，康熙的心里想的未必不就是“朕即国家”，但他显然比路易十四更具“中国特色”的“智慧”――― 经常作些仁君秀，既行专制之实，又享仁君之名。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 按照路易十四之后的法国启蒙思想家的“主权在民”思想，国家的主权属于人民，所以不是“朕即国家”，而应该是法国人民说的“我们才是国家”。当然，这种思想观念是路易十四的时代之后才形成的。在路易十四的时代，世界上其实还没有多少人能够区分君主、政府、国家的概念有什么不同。在中国，虽然先秦的孟子已有“民为贵，社稷次之，君为轻”的观念，但实际上秦汉以来的二千余年中，爱国即是忠君，忠君亦即爱国，君主与国家在观念上还是混淆不清的。直到西方思想传入之后，中国人对国家、政府(朝廷)、君主的概念才逐渐形成清晰的现代认识，这其中第一人当推梁启超，他是在经历戊戌变法失败流亡海外的痛苦之后，才获得这种认识的。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 梁启超指出，中国之所以积弱，根源之一就在于国人不能正确区分国家与朝廷的概念，以致爱国心没有用在正确的地方。国家是什么？朝廷又是什么？“今夫国家者，全国人之公产也。朝廷者，一姓之私业也。国家之运祚甚长，而一姓之兴替甚短。国家之面积甚大，而一姓之位置甚微。”中国有悠久的历史，唐虞夏商周、秦汉魏晋、宋齐梁陈隋唐、宋元明清，“此皆朝名也，而非国名也”。从殷族的商、姬族的周，到嬴氏的秦、刘氏的汉、李氏的唐、赵氏的宋、朱氏的明，还有蒙古人的元、满人的清，它们都是一族一姓的朝廷，而不是国家，都是一族一姓的私业，而非全体中国人的公产。然而，中国人常常将国家与朝廷混为一谈，梁启超认为，这是中国人的大患。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 国家和朝廷不分的不良后果，最明显的一点就是爱国变成爱朝廷，甚至变成爱领袖――― 君主。梁启超说：“试观二十四史所载，名臣名将，功业懿铄、声名彪炳者，舍翊助朝廷一姓之外，有所事事乎？其为我国民增一分之利益、完一分之义务乎？而全国人民顾啧啧焉称之曰：此我国之英雄也。夫以一姓之家奴走狗，而冒一国英雄之名，国家之辱，莫此甚也！乃至舍家奴走狗之外，而数千年几无可称道之人，国民之耻，更何如也！而我国四万万同胞，顾未尝以为辱焉，以为耻焉，则以误认朝廷为国家之理想，深入膏肓而不自知也。”二十四史中的那些将相们，他们为一姓之功业杀人，以“万骨枯”换取自己的功名利禄，这本来与爱国无关，但却被各王朝树立为爱国的模范，而国人因不能正确区分爱国家与爱朝廷的差别而跟着礼敬之颂扬之，实在是可悲可悯。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 比梁启超晚一些时候，陈独秀写过一篇题为《我们究竟应当不应当爱国？》的文章，文中说：“要问我们应当不应当爱国，先要问国家是什么。原来国家不过是人民集合对外抵抗别人压迫的组织，对内调和人民纷争的机关。善人利用他可以抵抗异族压迫，调和国内纷争；恶人利用他可以外而压迫异族，内而压迫人民。”所以，“若有人问：我们究竟应当不应当爱国？我们便大声答道：……我们爱的是国家为人民谋幸福的国家，不是人民为国家做牺牲的国家。”<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 国家的功能，如陈独秀所说，一是抵抗异族压迫，一是调和国内纷争，前者对外，后者对内。调和国内纷争是就消极方面来说的，积极方面国家还需履行一定的公共职责，如救灾、赈济等。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 国家功能的实现，须通过政府去完成。如果政府能完成国家功能，国家就是“为人民谋幸福的国家”；如果政府不能完成国家功能，国家则有可能成为“人民为国家做牺牲的国家”。人类历史实践中的普遍情况是，政府常常不能完成国家功能，或者完成得很差，这样就有可能出现有政府等于无政府，甚至有政府还不如无政府的状况。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 地理环境决定了中国是一个水旱灾害频发的国家。有一项统计说，中国在民国前的2270年中，见于官方报告的旱灾有1392次，水灾有1621次，可见年年有灾。因此，中国古代的政府最重要的一项公共职责便是领导抗灾，这可以说是政府合法性的基础之一，灾异现象历来也是帝王们最关心的事。清代的皇帝还要求各省大员定时汇报雨水、收成、粮价等情况，以便随时了解各省灾情和民生，如出现灾荒可以及时组织赈济、减免受灾地方的税赋。但是，从历史记载来看，受灾得不到及时救助的情况还是非常普遍。当大规模灾害出现而政府不能履行其职责时，灾民为了生存就会铤而走险，如明末李自成等人领导的农民起义，其主要活动空间是在陕西、河南，原因即是两省大旱，而明政府却不能组织有效的赈济，使得灾民成为流民，进而升级为暴民。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 一个社会，有许多涉及大范围、众多人群的公共事务是无法由其他社会组织去完成的，而只能是由政府去完成。一旦政府不能履行其职责，社会就会无序，公共利益就会受到侵害。比如食品安全、公共卫生安全、环境保护之类的公共事务都要由政府去完成。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 人类社会在发展过程中，曾经长期陷入一个难解的困境：即人们需要政府，但政府却不能履行人们期待的外而抵抗异族压迫、内而提供公共服务的国家功能，在很多情况下还常常演化成一个与民争利、侵害民权的组织。要使政府尽职尽责，人民必须有监督政府的权力，而最有效的监督方式是用投票的方式去选择政府的权力。人们有必要了解一个常识―――即梁启超所说的国家不是朝廷(政府)，朝廷可换而国家永存，人们应该爱的是国家而不是朝廷。<br />
&nbsp; &nbsp; <br />
