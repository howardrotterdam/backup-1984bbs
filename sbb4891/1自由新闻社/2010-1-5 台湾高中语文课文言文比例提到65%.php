													<h2>台湾高中语文课文言文比例提到65%</h2>
						
						
																											<div id="postmessage_414671" class="t_msgfont">　&nbsp;&nbsp;教育部公布「九九高中国文课纲草案」，确定高中国文科文言文比例上限，从一百学年起，由现行百分之四十五调高到百分之六十五，学生要多读九篇古文，并扩大文化经典取材，明定每册课文应选一课文化经典教材，选修科目增列国学常识。<br />
<br />
　 教育部昨天在北一女中举办高中国文课纲草案公听会，由课纲委员、玄奘大学中文系教授罗宗涛担任引言人，近百人与会。针对文言文比例提升，各界意见不一，第一线教师更关心每周只有四小时国文课，根本教不完。<br />
<br />
　&nbsp;&nbsp;全国家长团体联盟副理事长林文虎表示，孩子语文能力与教学方法有关，和教材无关。他反对增加国学常识为选修，有兴趣可到大学再学。政府如想改变就应提出具体研究数据，才能说服民众。<br />
<br />
　&nbsp; &nbsp;北市成功高中国文教师范晓雯认为，草案新增文言文比例最高可达六成五，让教学现场有更多弹性，但每周只有四节国文课，时间不够、范围过多，令人担心。大考中心副主任沈青嵩说，文言文改采弹性比例，对命题技术比较有利，未来大考中心命题不会超越课纲规定。<br />
<br />
　&nbsp;&nbsp;教育部课纲项目小组指出，提升文言文比例主要是延续九年一贯精神，国一百分之十五，国二百分之二十五，国三百分之三十五，才会订高一百分之四十五，高二百分之五十五，高三百分之六十五的比例。根据草案内容，九九国文课纲草案的特色之一是重拾经典文学，虽然文言文选文减为三十篇，但明定每册课文应选一课文化经典教材，选材范围包括诗经、左传、礼记、论语、孟子、老子、庄子、墨子等。选修项目新增国学常识，规定高一、高二至少要开一门选修课。<br />
<br />
　另外，新课纲重视学生写作能力，订定高一到高三上，每学期要有四篇写作练习，高三下也要写三篇。教师还要指导学生选读中外文学名著，并要求学生每学期完成一篇阅读报告。</div>

							
							
							
							
