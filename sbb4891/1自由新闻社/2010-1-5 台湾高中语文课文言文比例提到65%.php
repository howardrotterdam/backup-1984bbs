													<h2>台湾九八课纲的争议点及其背后的意识型态 　　　林丽云</h2>
						
						
																											<div id="postmessage_417051" class="t_msgfont">　　国文科九八课纲的争论点在于，文白比例，论孟是否列为必修，文言文选文四十篇中台湾题材八篇。对于这三个争议点，笔者拟就基层教师的立场提出意见。<br />
<br />
一、&nbsp; &nbsp; &nbsp; &nbsp; 文白比例：传承古代中国古代文化VS亲炙台湾现代文化与语文学习<br />
　　九八课纲文白比例为五十五％：四十五％；反对者主张文白比例应为五十五％：四十五％，甚至要回到八八课纲时代文言文占六十五％，强调本国语文教育不仅是语文教育，还背负着文化继承的使命，文言文是穿越时代人类的共同遗产。何以学习文言文，才能传承文化的使命？白话文就不是文化的载体吗？论者心中含藏着古代中国文化的优位性，进而轻渺台湾现代文化的价值，这是令人无法苟同的。贵古贱今，贵远贱近观念中，对自我的否定，对当代文化的漠视，心态之偏颇，已显然可知。 <br />
<br />
本国语文听、说、读、写的学习，乃是语文教育的重点。当今无论自然科学或人文科学，大多以白话文为论述工具，足见白话文乃是承载现代思想与知识最适当的工具。能迅速地吸收信息，灵活地运用白话文，来自广泛地接触白话文，因此白话文应是最佳的语文素材。此外，一课文言，教师教学所用时间，比一课白话多，文白比例，纯就课数比例而论，实不客观精准。 <br />
<br />
语文是有机的生命，随着时代转变，新的语言文字不断地诞生，许多古奥的词汇，在高中三年课文中只出现一次，也不可能出现于日常生活中，为了结合生活经验，让下一代大量接触现代语文已是全世界的趋势，香港、新加坡、马来西亚等地的华语教学都是以白话文为主的语文课程，中国文白比例为一比三，且大量介绍世界各国的文学作品，比台湾更白话，更世界化。主张提高文言比例者又说，中文是我官方文字，不宜与香港等地相提并论。难道白话文不是中文吗？或只有文言文可代表中文？反对者又说中国放弃的文化遗产，我们要视为瑰宝，加以发扬光大。其证据为何？重视文言文的台湾学子将比中国学生更有竞争力？笔者以为这种未经证实的论调充满自我安慰的阿Q精神，实难取信于他人。 <br />
<br />
文言文艰涩难懂的词语虚字，对学生而言彷佛天书一般，必须穿透层层文字障碍，才能登堂入室，窥见文章的思想内涵。耗费无数青春时光，所学又远离语文的实用。因为文言只能阅读，不可能应用于日常生活听、说、写之中。文言文虽然典丽优美，却与学生的生活经验脱节，例如大考试题取材古典诗词的四季景象，学生常苦于难以判断，因文言诗文描写的四季景象，并非台湾时空环境下的四季；其书写的自然情怀，亦非现实存在的大自然。且古代中国与现今社会迥异，学生实难从古人的思想中，认识复杂的人性，汲取解决当今问题的参考。而且学生必需学习的课程何其丰富广阔，大人们忍心为了自己古典中国文学之梦，而加重孩子身上的负荷吗？二○○六年PISA评比，台湾十五岁学生的语文阅读能力为第十六名，不及日、韩，语文教育应当落实白话文阅读能力的培养，进一步提供共同的阅读书目，要求学生大量阅读，书写阅读报告或作问题讨论，提升学生吸收信息，欣赏作品的能力。 <br />
<br />
二、 论孟为选修或必修：独尊儒术的文化霸权VS以台湾为主体，尊重教师专业自主 <br />
　　九八课纲文化基本教材为选修，选修科目除论孟选读之外，尚有区域文学选读、小说选读、语文表达及应用；反对者主张将论孟列为必修或必选，其理由是国文教育担负着人格教养的责任，论孟的道德思索与生命哲理，是品格教育的最佳素材。就品格教育而言，难道论孟是培养学生品格唯一且必要的经典吗？试问蒋介石时代极力推行儒家思想，有碍其白色恐怖的推行吗？当年你我埋首钻研文化基本教材之际，背后的推手道德吗？中国近年来重视儒家思想与传统文化，在全世界广设孔子学院，然而他的企业家却以毒奶粉残害以牛奶为唯一主食的下一代。欧美国家人民从未接受儒家思想的熏陶，他们的国民比我们不道德吗？他们的人格不如我们高尚吗？论孟岂是品格教育唯一且必要的素材？国文课本中大多数的选文无论文白，皆可作为品格教育、生命教育、生死关怀的素材，端看教师是否具备这方面的学养与觉知，能否融入生活，灵活应用而已。<br />
<br />
反对九八课纲者独尊中国儒家思想。实质上，台湾文化是多元的，中国文化是台湾文化很重要的一部分，但仅是台湾文化的一部而已。中国文化除汉族之外，尚有满、蒙、回、藏的文化，因此课纲也提醒当将原住民的作品纳入教材之中。除儒家之外，道家、墨家、法家亦有其值得取法之处，因此九八课纲列出文化经典，每册介绍一家的学术思想，即源于广纳诸子百家，尊重多元思想的构思。 <br />
　<br />
就现代公民的养成而言，孔子主张「子为父隐，父为子隐」，重视伦理胜于国家法治，欠缺现代法治观念。现代社会众声喧哗，思想多元，公民最需要的是尊重异己的涵养，伏尔泰说：「我虽然不同意你的意见，但我愿意为你争取发言的机会」，表达打开心胸聆听、理解，包容不同的意见的雅量。孟子滕文公下：「圣王不作，诸侯放恣。处士横议，杨朱、墨翟之言盈天下；天下之言， 不归杨则归墨。杨氏为我，是无君也；墨氏兼爱，是无父也；无父无君，是禽兽也」。孟子斥责墨翟无父，杨朱无君，表现难以接纳异己的思想。《孟子&#8226;滕文公上》：「今也，南蛮`舌之人，非先王之道，子倍子之师而学之，亦异于曾子矣」。嘲笑南方偏远农家思想家许行，充满华夏文化的优越感，都与现代公民涵养冲突。 <br />
<br />
文化基本教材列为选修，意味着尊重教师专业自主，给予教师更多自由发挥的空间，满足学生的需要，如此有何不妥？若教师以为论孟是最重要的经典，可以展现自由意志，选修论孟，并无任何阻碍限制；如果当地有特殊的文学风貌，或教师擅长小说，或学生需要加强语文表达与应用，而愿意开小说选读，语文表达或区域文学等课程让学生选修，有何不可？强制规定文化基本教材为必修，则区域文学、小说选读、语文表达与应用完全被封杀出局，教师与学生无自由选择的余地。 <br />
<br />
二、&nbsp; &nbsp; &nbsp; &nbsp; 台湾题材八篇：反台湾主体意识VS台湾主体意识 <br />
　　张晓风女士曾批评东番记出现许多番字，充满对原住民的歧视；笔者以为在此正可教导学生，世界各国对原住民的尊重是随着人权的发展慢慢演进的，当以作者生存的时空背景来解读。陈第东番记、郁永河裨海纪游选、陈梦林望玉山记、蓝鼎元纪水沙连四篇以台湾人文地理为主；郑用锡劝和论、连横台湾通史序、郑用锡劝和论、吴德功放鸟、蒋渭水快入来辞，以台湾的历史人文为主，各有其写作的时代背景，都是认识台湾文化的重要文章。<br />
<br />
　　东番记叙述平埔族的民族性，生活习惯，风土民情，婚姻、丧礼。写到平埔族喜爱鹿肠子中刚咽下的将变成粪而尚未变成粪的「百草膏」，不喜欢吃鸡和雉，只拔它们的羽毛来装饰，看到华人吃鸡就反胃呕吐，作者说：「谁知何谓正味呢？人的口味哪有相同嗜好呢？」引用庄子的思想，表达尊重平埔族的饮食文化，不以汉人的饮食习惯论断他族的涵养。郁永河裨海纪游选北投采硫，描写北投硫土产地独特的地貌，沿途山林、楠木的景象，兼顾视觉、听觉、嗅觉、感觉，刻划逼真生动，文采优美。陈梦林望玉山记，书写象征台湾精神的玉山，玉山之名，玉山之高峻遥远，玉山瞬间风云变幻的绝美景象，其神秘缥缈，可望而不可及，乃作者平生所仅见。蓝鼎元纪水沙连，描写浮出水面的水沙连风光之美，水沙连浮田种植稻子，水深鱼美，原住民挟弓射鱼，收获丰硕，原住民享受鱼肉、美酒，高歌欢唱，快乐无忧，实不知帝王的力量对他们有什么作用。羡慕原住民置身世外桃源，生活纯朴喜乐。往昔日月潭水沙连的景象在作者笔下原貌重现，令人无限神往。　 <br />
<br />
　　漳泉分类械斗起，郑用锡亲赴各庄，努力排解，着这篇劝和论晓谕众人。他认为人心之变，从分类开始。分省籍，分先来后来，但是大家「同自内地播迁而来，则同为台人而已。」选择本文者，怀有深刻的用心，乃期待台湾族群融合，不再区分本省、外省、客家、福佬、原住民，体认共同的命运，建立国家的认同。连横台湾通史序，简介台湾历史，强调「台湾人不可不知台湾事」，提醒国人认识自己的历史，才能继往开来，为后代子孙建立更美好家园，不负祖先渡海来台，「筚路蓝缕，以启山林」的艰辛。吴德功放鸟，因鸟长久关在樊笼里，必然违逆本性，放鸟，希望它顺着本性生活。藉鸟不愿离去，譬喻士人贪图安逸，自甘败弃。蒋渭水快入来辞，模仿陶渊明归去来辞「快入来兮，心园将芜胡不入，己自以身为奴役，奚惆怅而独悲。」反对日本当局的殖民意识，他甘心乐意为此被关入监狱，监狱之中正是他修身养性、磨练心智之地，真是深有涵养，从容优雅的人格者。<br />
<br />
　　八篇台湾文言占文言四十篇的五分之一，比例太高吗？从明清以来书写台湾的古典诗文不可胜数，而年轻的一代生长于此，亲炙先人书写的台湾史地人文，不也是天经地义的吗？笔者以为古典诗词不当将台湾素材排除在外，应包含有关台湾素材的诗作。 <br />
<br />
　　反对九八课纲者以为这八篇文章对教师而言十分陌生，且文字不够优美。教育当局长久以来未能以台湾为主体，未曾纳入学生学习教材所致。如今列为选文，正是接触与学习的良机，而今而后，年轻一代则不至于觉得陌生。何况教学本来就是不断学习与提升的过程，教材更新或新增，带来的只有令人振奋的喜悦。至于文采不够优美的质疑，笔者以为选文不当以文采华美为唯一首要的考量，文章包含感性精致的纯文学作品与理性或知识性的论述，如史地等人文科学、科普论述，则以文辞达意，论述清晰为重，这八篇台湾素材何尝不可以这样的选文标准来看待？何况这八篇文章虽非通篇皆美，行文之间也有可观的文采。 <br />
<br />
四、其它反对的理由：语文能力退步、白话文的地域性与时代性 <br />
　　九八课纲尚未实施，反对者即已认定台湾的中学生语文能力退步，乃因减少文言文之故。学生语文能力果真退步了吗？以报纸上几位被评审教授讥讽的考生为论断吗？笔者执教以来，常感觉这一代的学生比我们那一代强多了。高中时，作文簿老师的评语永远只是好、通顺、尚可，总摸不清文章当如何写，如今学生在短短的应考时间，文章写得情真意挚，优美动人。教师教学的方法与技巧与日俱进，学生也一代比一代更聪明，更有创意。再者学生语文水准和文言文有绝对关系吗？学习白话文将使学生语文能力低落吗？语文教育在培养什么能力？运用文字的能力是基本的能力，语文能力的高低，不仅在于运用表达工具的能力而已，而在于学生知识是否渊博？是否有个人的思想见解？是否有深刻的人文关怀？重要的是内涵。学生文章若不佳，并非不能遣辞造句，而是没有丰富的知识与深刻的思想见解。 <br />
<br />
　　此外，反对白话文的另一个理由是地域性与时代性。张晓风女士指出「部分白话文有地方性及时代性，反而不如明清之后的文言文容易懂」。试问世界上哪一个国家的文学作品不是从土地产生出来的？那一个作家不是立足在自己的土地上从事文学书写的？土地是我们安身立命之处，是我们最深的爱恋，也是我们创作时灵感的源泉。那一个作家的作品没有时代性？不具地方性？有哪一篇文学是空思幻想产生的？即使是想象的魔幻小说如哈利波特也是从现实的土壤萌生的。白话文有其时代性与地域性不是理所当然的吗？而竟然可以成为被反对的理由。如此著名的散文家竟然会因为白话文有地方性及时代性而看不懂？难道张女士在台湾这个土地、这个时代生活了五十多年，却还不了解台湾人的生活与文化吗？进而「推己及人」，认为别人也不了解？自己不了解，因而拒绝接触；所以「推己及人」，也不许下一代接触了解？ <br />
<br />
五、争议背后的意识形态 <br />
（一）以中国文化为主体VS以台湾文化为主体 <br />
　　任何一种主张，都有其思想基础，或所谓的价值观，更直接说即是意识形态。无须遮掩粉饰，请直接摊开来表述。 <br />
<br />
　　反对者认为提高文言比例，论孟列为选修，增加八篇台湾题材的文言选文，是意识型态作祟，是去中国化的作法；笔者以为提倡文言文，尊崇儒家思想，则是以中国文化为优位，意图改变台湾主体性的性格，是去台湾化、去现代化、去生活化、去世界化的意识型态作祟。 <br />
<br />
　　有人以为提高白话文比例，论孟列为选修，即是剥夺台湾孩子的文化继承权，意指中国文化即等同于台湾文化。这绝非事实，也是无法被接受的。台湾的历史与文化既特殊又多元，平埔族的文化是台湾相当早期的文化，十七世纪荷兰人的统治，也对台湾文化产生影响，如今嘉义兰潭尚有「红毛埤」，乃是荷兰时代挖掘的水塘。台湾最早的学校，是1636年荷兰人设立的，教导人民以罗马字拼西拉雅字，乃是台湾第一次出现的文字，且当时已经有以罗马拼音翻译马太福音的西拉雅文。台湾民谣「安平追想曲」描述荷兰军医与台湾少女的爱情故事，也是人们耳熟能详的。1652年沈光文来台，台湾才有汉文，中国文化是后来者。西班牙占领淡水，至今尚有红毛城遗迹。清代汉人移民台湾，不许携家带眷，娶妻不易，因而有「娶个某，胜过三个天公祖」这种趣味横生的俗谚。「有路没厝」的罗汉脚，死路旁，死没人哭，由当年独身男人不幸的遭遇，后来成了骂人的话语。明清时代的河洛人或客家人的汉诗，日治时代的中文或日文写作亦不少，甚至战后来自中国的新文学作品，都可以让学生了解台湾文学的多元性格，以及这块土地的台湾历史与文化现象。若将中国当作全体，则漠视了台湾历史发展的事实；强加下一代某种单一文化，才真是剥夺孩子接触台湾其它文化的机会。 <br />
<br />
（二）不当剥夺台湾孩子认识自己土地的权利 <br />
　　若反对九八课纲者不认同台湾是一个国家，期待来日终极统一；也当肯定脚下让我们安身立命的土地以及二千三百万个同胞。如果您不愿意认识这块土地的过去，不想了解台湾人的文化，不想读懂台湾人的文学作品；也请你容让下一代孩子认识他生长土地的历史：那最早的原住民平埔族，水沙连的地理景观，彰泉争斗的历史，反殖民统治的先辈蒋渭水的人格风范，让他们读得懂台湾前辈作家赖和、钟理和的作品。那是他们不应被剥夺的权利。如果您认为现代作家无阅读的价值；请您也容让下一代接触书写他们的时代与社会环境的作品，认识当代的作家。那是他应当认识的，因为他若与自己生存的时空环境隔绝，从现实中被抽离，他将失落了自我。如果您认为孔孟是培养道德唯一且必要的经典，您可以躬亲实践；但是请容让教师与学生有选择的自由，因为区域文学可以带领他们认识故乡作家的作品，彰化的孩子可以阅读赖和的作品；美浓地区的孩子可以研究钟理和的著作，踏着先人的脚步，感受他们对自己熟稔的土地与人民，如何取材书写，文学的命脉因此可以代代传承。小说的世界何等宽广，可以了解故事发生的时空舞台，认识那一个时代的重要议题，欣赏人物的刻划、人性的复杂幽微，人我关系的奥妙，学习小说家巧妙的布局。人格教育，不一定得用道德箴言直接来教导训诲，小说故事生动的演出，自然地点破人性的弱点，对学生有极为深刻的启发。也许您会说这一切学生可以在课后学习；但为什么该成为课后学习的不是文言文？何不让钟情古典中国文学的人课后去钻研文言文呢？ <br />
<br />
　　国民教育的目的究竟是什么？即是养成国民阅读一般读物、表达和欣赏的能力，培养善美情操的认同，得以看到世界的美好；教科书的功能，是为了让国民认识本国作者和作品。教育的重点在于国民人格的养成目标，让他们先成为一个人，其次成为认识自己社会时代的台湾国民，最后成为世界公民。</div>

							
							
							
							
