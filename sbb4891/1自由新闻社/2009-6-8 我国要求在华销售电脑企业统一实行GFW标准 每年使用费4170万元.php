													<h2>我国要求在华销售电脑企业统一实行GFW标准 每年使用费4170万元</h2>
						
						
																											<div id="postmessage_114920" class="t_msgfont">中国收紧互联网控制<br />
2009年 06月 08日 08:43<br />
&lt;华尔街日报&gt;<br />
<a href="http://chinese.wsj.com/gb/20090608/chw085100.asp?camp=china" target="_blank">http://chinese.wsj.com/gb/20090608/chw085100.asp?camp=china</a><br />
<br />
<br />
中国计划要求7月1日之后在华销售的所有个人电脑出厂时装有阻止电脑访问某些网站的软件，此举可能会给政府互联网内容审查机构对中国网民访问互联网的方式施以前所未有的控制。<br />
<br />
政府已经将这个要求通知国际个人电脑制造商，但尚未公布。政府表示，此举旨在保护未成年人不会接触到“有害”内容。<br />
<br />
这项规定可能会迫使个人电脑制造商要么选择在一个重要市场拒绝政府的命令，要么选择让自己暴露于为审查推波助澜的指控中。数家外国企业已经被批评说，它们顺从了中国政府的审查要求，以便能够在中国继续经营下去。<br />
<br />
中国政府广泛审查网络内容的历史引起了一些外国业内管理人士和美国政府的担忧，他们担心新的措施可能会显著增大政府对中国互联网访问的控制。业内管理人士也警告说，这类软件可能会导致在中国运行的个人电脑发生故障，可能会令电脑更容易受到网络黑客的攻击。<br />
<br />
美国驻华使馆发言人史蒂文森(Susan Stevenson)说，我们正在研究这项新的规定，评估它的影响；我们将密切关注任何试图限制信息自由流动的举措，并将其视为有悖于中国建立一个现代化、信息型经济和社会的雄心。<br />
<br />
据一名为政府部门测试了新软件的官员说，软件将把个人电脑与一个更新的被禁网站数据库连接起来，阻止电脑访问这些网络地址。<br />
<br />
这个软件的中文名称是“绿坝-花季护航”。中文中的“绿”用来表示没有色情等其他违法内容的网络浏览。<br />
<br />
该软件由金惠计算机系统工程有限公司开发，输入内容来自北京大正语言知识处理科技有限公司。这两家公司均与中国军方和安全部有联系。金惠表示，“绿坝”的运行方式类似于其他国家旨在让家长阻止电脑访问儿童不宜的网络内容的软件。<br />
<br />
已经检查了“绿坝”的外国业内管理人士说，个人信息可以通过这个软件进行传送，用户将很难确切知道哪些内容被阻止了。<br />
<br />
软件开发商金惠的创始人Bryan Zhang说，公司计划通过一个互联网更新系统将新的被禁网络地址传送给用户的个人电脑，这类似于操作系统软件和防病毒程序的更新方式。<br />
<br />
Zhang说，他的公司汇总和保存被屏蔽网站的名单，他说目前遭到屏蔽的仅限于色情网站。但这种软件使得对其他网站实施限制成为可能。<br />
<br />
工业和信息化部5月19日发布的通知中概述了这个规定，不过尚未被媒体报导。《华尔街日报》看过这个通知的一个复印件。通知中说，个人电脑制造商在7月1日之后必须在在华销售的个人电脑上预装“绿坝”软件。<br />
<br />
通知中说，软件必须要么预先安装在硬盘上，要么有随机附带的光盘。通知中说，个人电脑制造商必须向政府上报预装有该软件的个人电脑发货量。<br />
<br />
尚不清楚中国政府可能如何执行新的规定。尽管通知中没有提及任何惩罚措施，不过对个人电脑制造商的违规后果的担忧可能足以确保厂商遵守规定。<br />
<br />
去年中国个人电脑销量接近4,000万台，仅次于美国。如果制造商不遵守规定的话，受到影响的不光是它们的市场份额──大型个人电脑公司还在中国投资设立了工厂和研究设施。<br />
<br />
据研究公司国际数据公司(IDC)的数据，中国企业联想(Lenovo Group Ltd.)的市场份额最大，2009年前三个月的发货量占市场总量的26.7%，而惠普(Hewlett-Packard Co.)和戴尔(Dell Inc.)的份额分别为13.7%和8.1%。<br />
<br />
惠普发言人说，该公司正与政府有关部门一起评估最佳解决办法。她说：显然，我们将专注于提供最佳客户体验，同时确保我们满足必要的监管要求。惠普是全球发货量最大的个人电脑销售商。<br />
<br />
包括戴尔、宏?(Acer)和联想在内的其他大型个人电脑销售商的发言人要么说他们不知道有这样的要求，要么没有回复记者就软件发表置评的请求。<br />
<br />
外国业内高管说，几乎没有给公司多少时间来适当地测试“绿坝”软件。一位与数家大型个人电脑制造商讨论了计划的业内高管说，缺乏透明、实施时间短、全球各地都望尘莫及的令人难以置信的限制范围，这些都给业界带来了巨大的挑战。<br />
<br />
中国已经在运行一个通常被称为“防火长城”(Great Firewall)的大规模互联网过滤系统。专家们说该系统是同类中最复杂的。它屏蔽一系列的内容，从色情网站到政治敏感性网站。这类网站包括那些宣扬西藏独立、轮子的网站，某些情况下还会屏蔽外国媒体网站。<br />
<br />
但这一系统是在网络层面上屏蔽内容，许多用户可以通过代理服务器等工具绕过这一系统。已评估过“绿坝-花季护航”软件的外国业内高管说，通过将内容过滤功能扩展到每一台电脑，这款软件可能使政府得以收紧对互联网的控制。<br />
<br />
一位熟悉这一计划的外国业内高管说，没人确切知道这款软件功能的涵盖范围。他说，虽然自己不认为有谁会反对政府屏蔽色情和暴力内容的公开目标，但人们对政府实施屏蔽的方式感到非常担忧。<br />
<br />
使每一台电脑都与一项通用性软件相连接，也会让这些电脑更易遭受网络攻击。业内管理人士们还说，被设计与微软公司Windows操作系统一道工作的“绿坝-花季护航”软件有可能与其他应用软件发生冲突，从而引发电脑故障甚至系统崩溃。<br />
<br />
中国工业和信息化部没有回复记者要求置评的电话。中国软件评测中心的吴娓娓说，这款软件经广泛测试未发现问题。吴娓娓负责这款软件的测试工作。<br />
<br />
政府要求新电脑预装“绿坝-花季护航”软件的通告说，此举旨在创造一个绿色、健康、和谐的互联网环境，防止互联网上的有害信息影响和毒害青少年。<br />
<br />
这份通告说，政府已经为中国所有电脑使用这款软件支付了一年的费用。<br />
<br />
金惠的管理人士Bryan Zhang说，该公司的这一产品与美国市场出售的父母控制子女上网软件类似。<br />
<br />
他说，如果父母想接入那些被屏蔽的网站，“绿坝-花季护航”软件可以被关闭，而且这一软件可以被卸载。用户如果想卸载这一程序，他们需要输入一个在安装这一软件时设置的密码，设置密码是为了阻止孩子们让软件失效。<br />
<br />
