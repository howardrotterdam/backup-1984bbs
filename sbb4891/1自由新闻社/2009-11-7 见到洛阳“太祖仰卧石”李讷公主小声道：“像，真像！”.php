毛泽东主席女儿李讷一行到嵩县天池山看伟人卧像与公心峰前后<br />
<br />
今年8月份，洛阳九九龄醋业集团董事长王体军给我打电话说：“今年10月下旬，将邀请毛泽东养生饮食文化研究会研讨会第九届会议在嵩县天池山召开，届时毛主席的女儿李讷会参加。”我听后很高兴的说：“感谢董事长，我们会全力支持配合。”之后，我安排天池山管理局接待科科长张谊敏在与我们有合作关系的《大河报》、《东方今报》、《郑州晚报》等新闻媒体上发布有关信息。 <br />
<br />
8月25日，王体军董事长打电话给我说：“把你们的宣传彩页送来一部分，我们要去北京联系有关事宜。”我便安排张谊敏科长送去了100张宣传彩页。需要说明的是，天池山发现“伟人卧像”、“公心峰”后，原嵩县县委副书记张志新、原嵩县旅游局局长仝宇鹏、嵩县旅游领导小组组长常务副县长郭祖勋、嵩县总工会主席、旅游局局长苏焕章、天池山管理局局长王孝森等曾先后到北京专程给李讷、李敏等毛主席家属送过“伟人卧像”、“公心峰”图片，邀请他们来看天池山。 8月25日以后，我多次和体军董事长联系，他说：“正在运作，主要通过毛主席侄女毛小青邀请，初步打算邀请李讷、李敏，毛主席孙子毛新宇，朱总司令儿媳赵力平，朱总司令孙子朱全华等人参加第九届毛泽东养生饮食文化研究会研讨会。”<br />
<br />
10月15日，王体军董事长打来电话说：“已经初步定下来了，毛研会来52人，湖南毛主席家乡来20多个人，毛主席、周总理、朱总司令的亲属及身边的工作人员都确定下来了，李讷一定来。”10月18日上午，王体军董事长带领毛研会副会长李学进等人到天池山采点，我们进行了认真研究，确定了有关行程及安排。期间，我多次向局长王孝森汇报，天池山管理局多次开会认真研究，拿出了一套比较完整的方案，向县主要领导及主管领导汇报，县委书记马振宇、县长李大伟、县总工会主席、旅游局局长苏焕章都高度重视，亲自指导接待事宜及有关细节。 <br />
<br />
10月24日上午，县领导和天池山管理局局长王孝森到县界迎接李讷等毛研会成员一行，县委书记马振宇、县长李大伟在天池山景区中心广场准备欢迎仪式。10点20分左右，客人还未到，景区内突然下起了小雨，人们也顾不上避雨，继续准备欢迎仪式。10点39分，李讷一行到达天池山景区中心广场，这时我突然发现雨停了下来，太阳又出来了，广场上响起了“东方红，太阳升，中国出了个毛泽东……”的嘹亮歌声。县领导及天池山的工作人员有秩序的站到了预先安排好的位置，礼仪小姐向李讷、赵力平献花，县委书记马振宇、县长李大伟迎上去和李讷、王景青、赵力平等亲切握手，并接他们进入天池山景区游客接待大厅，游客们及工作人员抑制不住内心的激动，广场上响起了雷鸣般的掌声。在接待大厅内，马书记、李县长和李讷、王景青、赵力平等亲切交谈，介绍嵩县天池山景区的“伟人卧像”和“公心峰”等有关情况。之后，在县委领导的陪同下，李讷一行到天池山飞来石景区观看了“伟人卧像”和“公心峰”景观，在“伟人卧像”观景台，《东方红》歌曲不时的播放着，看着眼前的“伟人卧像”，有人问李讷女士，像不像主席，李讷小声的说：“像，真像！”看了公心峰后，李讷、王景青夫妇抑制不住内心的激动，李讷女士说：“太神奇了，一定要把‘公心’两个字照下来。”在“伟人卧像”、“飞来石”、“公心峰”观景台，李讷、王景青夫妇都平易近人，有人想和他们合影留念，他们都一一答应，和游客们留下了许多珍贵的记忆。我在中心区等候毛泽东养生饮食文化研究会大部队的到来，换乘小交通车辆后也到达飞来石景区。毛研会会长、毛主席生活管理员吴连登、毛主席的侄女毛小青，卫士周福明、钱嗣杰，警卫战士赵延河、周总理身边工作人员朱金顺都在队伍中间，在“伟人卧像”、“公心峰”景点合影留念。从主席身边工作人员合影的过程中，我深刻的体会到主席在每个人心中的份量，游客们也争相和毛主席、周总理、朱总司令的亲属和身边工作人员合影留念，景区的工作人员和游客们的兴奋不言而喻。中午，县委、县政府在碧云山庄举行了隆重的接待宴会，在熟悉的东方红歌曲声中，人们逐渐安静下来，县委书记马振宇发表了热情洋溢的祝酒词，表示对客人们的欢迎。下午3点30分左右，李讷女士离开景区入住洛阳。4点30分，第九届毛泽东养生饮食文化研究会研讨会在碧云山庄会议室召开。会议由毛研会秘书长尹振凯主持，县总工会主席旅游局局长苏焕章代表县委、县政府对大会的召开表示祝贺，并向毛主席的侄女毛小青、朱总司令的儿媳赵力平赠送了“伟人卧像”、“公心峰”画框。之后，由讲解员为与会人员讲解嵩县旅游资源及嵩县县情，与会人员对毛主席的养生饮食文化进行发言交流。会上毛小青女士动情的讲：“以前看到过有关图片，今天看了‘伟人卧像’、‘公心峰’，真是百闻不如一见，大自然真是造化神功，这也是对主席老人家一生最公正的展示和评价。” <br />
<br />
10月24日晚上，吴连登会长特意邀请了全国口技绝活演员南卫东先生为毛研会助兴，天池山管理局、碧云山庄的工作人员密切合作为毛研会献上了一场精彩的篝火晚会，人们载歌载舞，尽情地表达着对大好河山和新生活的热爱。晚会结束后，吴连登会长等人挥毫泼墨，为天池山题词。毛小青女士写了“奇石灵山秀水天池山”、“公心为民”，吴连登会长写了“思源”，毛主席生前警卫战士赵延河欣然赋诗一首《天赐嵩县&#8226;七绝》：伟人长卧天赐山，公心为民乃大贤；九龙朝圣毛泽东，丰碑永立人心间。值得一提的是，毛主席、周总理、朱总司令的亲属及身边工作人员都和蔼可亲、平易近人。由于我的工作是安排他们的生活，所以和他们的接触多点，毛小青女士闲暇时，我拿出纸说：“您给我们天池山写点字吧？”她很亲切的说：“写什么呢？”我说：“奇石灵山秀水天池山”她很愉快的写了。我说：“给我的孩子也写个字吧，鼓励他好好学习。”她当即写下“赠王韶同学：好好学习、天天向上。”朱总司令的儿媳赵力平女士已经80多岁了，也非常和蔼，热情地给我们留字。 <br />
