<br />
　　今年来，一个名叫“绿土地健康社区”的组织，悄悄在厦门广播山开班授课。这是一个极具迷惑性的组织，根据它的网站介绍，其主要活动内容包括组织共同生活、周末舞会、生态旅游、有机农业等等。<br />
<br />
