<br />
云顶山下有群特殊农民<br />
高学历青年抛弃城市，来厦试验有机农耕<br />
<br />
<br />
<br />
<br />
2006年，中国最流行的生活语录是：农夫，山泉，有点田。当很多人还在向往这种回归自然的生活时，几个厌倦城市的女白领却早已实践了它。2004年，她们放弃城市的工作，成立了“绿土地健康社区”，过起了流动的“农耕生活”，足迹辗转重庆涪陵、云南丽江、重庆綦江。<br />
经过《北京青年报》、《鲁豫有约》等媒体的报道后，“绿土地”备受社会的关注和争议。如今，他们落户到了厦门，在同安汀溪镇某村庄延续着独特的生活方式。上周，记者走近了这群特殊的“农民”。<br />
新社区：共同劳动与生活 资产共有<br />
“绿土地”的新家位于厦门最高的云顶山下，汀溪水库的上游。他们选择来到厦门正是看中这里“水源干净，没有工业污染，有可以耕种的地”。<br />
“榆柳荫后檐，桃李罗堂前”，走进“绿土地”的新家――― 坐落在小山坡上的闽南老厝，脑中浮现出了陶渊明的诗句。堂前舍后生长着桃花树、桂花树、龙眼树，后院有个菜园子，菜地旁是他们用竹子和塑料布搭建的三个简陋厕所，粪便都用管子引到化粪池里，成为有机农耕的好肥料。<br />
“我4年没吃过药，能背100多斤的重物上下山；每天都很开心，一切负面情绪都在哈哈大笑中被丢开。”成员林芝萍毕业于厦大无线电物理系，曾供职于联通公司，现在的她黑黑瘦瘦的，卷着裤腿，看起来跟一般农妇形象无二。<br />
每天，跟其他人一起，林芝萍准时五点半起床，以30分钟的哈哈笑来开始一天的工作和生活。到了傍晚，大家会进行Kundalini（生命源泉）锻炼，伴随着音乐，身体轻柔舞动；晚上则召开“社区会议”。据介绍，社区成员除了各展所长，也会轮流从事各项工作，每个人都是个“多面手”。<br />
社区成员年龄都在二三十岁左右，浙江人张芙媚很热情，一点都看不出她曾是个备受抑郁症困扰的大学生；重庆人夏艳飞因厌倦城市“喧嚣”而走进社区，她说自己从未想过再回到城市生活；江苏人贾秋南是名新成员，曾在国企从事计算机维护工作，厌倦枯燥的工作而于2008年初加入到这个行列中。按照他们的说法，社区里没有城市的贪婪、尔虞我诈，大家共同劳动共同生活，资产共同拥有，成员自由流动。当记者问如何界定成员关系时，他们说：“我们无话不说，比亲兄弟姐妹还要亲！”<br />
新农耕：拒绝化肥与农药 有机种田<br />
离老厝不远的山坡下，有一小片“绿土地”有机农耕的试验田，这是一块面积不到一亩，因土壤酸化、板结，而被当地农民废弃了的土地。已是种田老手的成员们用畜便、秸秆、草、枯叶等有机废弃物，加上微生物，经过发酵制成有机堆肥；定期给作物添加肥料、微生物和石头粉，并且不喷洒农药。如今土质已有些好转，地里的蔬菜长势良好。不过，“现在每天都有很多鸟来啄菜”，成员们有点烦恼，“也许鸟也尝出来这菜比较甜”。<br />
但是，因为太差的土质改良需要花三年时间，所以“绿土地”的农业收成并不算好。而且，有机种田不是一件独善其身就可以做好的事，“绿土地”的成员们积极跟当地村民沟通，想让村民接受“有机种田”的理念，将技术推广开来，并包销村民的有机农作物。目前已有部分村民接受了这种理念，还向他们学做有机堆肥。<br />
缺乏稳定的经济来源，为了维持社区生活，“绿土地”还得依靠开办社区体验等收费项目来增加收入。同时，“开展生态旅游”也提上了他们的计划表，依托当地的旅游资源，建立乡村酒吧、陶吧……<br />
新观念：我的生活我做主<br />
“绿土地”的成员都接受过高等教育，他们选择过自己想要的生活，来自外界的压力自然不小，最主要的是来自家人。大多数成员的父母无法理解，却只能无奈接受。对此，成员的态度是 “不愿意活在别人的期待中”，“如果连自己都照顾不好，怎么顾及家人”。<br />
有人质疑他们消极生活、脱离社会。成员们否定了这种说法。“我们一直和社会保持密切的联系。”从成立最初，“绿土地”就对外提供免费的“哈哈笑公开课”以及收费性质的 “生命能量唤醒”等心理治疗课程。“很多人从我们的治疗课程中受益，特别是一些心理受过伤害的人，”夏艳飞说，“其实这也正是我们用自己的方式尽着社会义务。”<br />
关于未来，成员们有个设想：当社区蓬勃发展时，可以分化出新的社区，使每个社区保持在20人左右，以便大家能保持家人般的关系。而所分化出的新社区，可以选择在新的城市发展，同时大家也可以自由选择在哪个社区生活。<br />
