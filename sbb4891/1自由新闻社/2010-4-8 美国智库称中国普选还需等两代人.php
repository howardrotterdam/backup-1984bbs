																											<div id="postmessage_554707" class="t_msgfont">【联合报u国际中心／综合报导】 2010.04.08 03:06 am <br />
 <br />
&nbsp;&nbsp;<br />
中国旅美学者、华府智库布鲁金斯研究所资深研究员李成六日表示，台湾是中国民主化的动力，不过中国人民要自己选国家主席，不会是五年后也不会是五十年后，恐怕还得等上一、两代。<br />
<br />
中央社报导，布鲁金斯研究所六日举办「海归派对中国政治影响」研讨会，应邀担任主讲人之一的李成在会后表示，大陆改革开放卅年来，出国留学学生已达一百六十万人，其中五十万人已学成归国，且越来越多加入领导团队。<br />
<br />
例如第十六届中共中央委员海归派只占百分之六点二；第十七届已增加到百分之十点五；预计二○一二年第十八届可再上升到百分之十五到十七。<br />
<br />
毕业自华东师大、先后在柏克莱加州大学及普林斯顿大学获得硕士和博士学位的学者李成指出，随着研究社会科学留学生回国人数不断增加，中国政治体制势将受影响，促使中国民主改革。<br />
<br />
李成说，中国迟早要走上公正选举、法律治国和媒体自由这条民主之路。至于要花多少时间、人民什么时候才能自己选国家主席，他说不会是五年后也不会是五十年后，可能要等上一、两代。<br />
<br />
李成并说，台湾也是中国走向民主化的动力。他说，台湾许多民众认为，只有中国大陆民主化以后，台湾才可能与大陆进行政治和谈。他认为这是对的，这也是对大陆民主改革的一股推动力量。<br />
<br />
对于两岸未来到底要何去何从？李成说，两岸的分合取决于许多因素，包括国际形势、大陆本身变化及台湾民众在许多问题上的新看法等。不过总体来看，他认为两岸关系是乐观的，至少不会有战争。<br />
<br />
李成并说，由于两岸都有许多留学生，说不定未来两岸领导人是同班同学，而且是好朋友，这不是不可能。一旦这种事情发生，对两岸关系将很有意义。</div>

							
							
							
							
