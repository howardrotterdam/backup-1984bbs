																											<div id="postmessage_537845" class="t_msgfont"><a href="http://news.ifeng.com/mainland/201003/0329_17_1589936.shtml" target="_blank">http://news.ifeng.com/mainland/201003/0329_17_1589936.shtml</a><br />
&nbsp; &nbsp;&nbsp; &nbsp; 记者从上海世博会安全保卫工作指挥部获悉，上海世博会期间，本市将对管制刀具和具有危险性的其他刀具加强重点安全管理措施，切实维护社会公共安全，保障市民和境内外游客的人身安全。<br />
<br />
上海世博会期间，本市重点加强安全管理的刀具范围为以下两类，一类是纳入国家管制范围的刀具，如匕首、三棱刀、带有自锁装置的弹簧刀等；一类是管制刀具以外的菜刀、大型水果刀、工艺品刀、陶瓷刀等具有危险性的其他刀具。<br />
<br />
记者从有关部门获悉，上海世博会期间，本市禁止销售管制刀具和陶瓷刀，对其他危险性刀具实行定点销售制度，刀具销售单位必须取得区、县商务部门颁发的《世博期间刀具销售定点单位》证后，方可销售。未取得《世博期间刀具销售定点单位》证的销售单位必须将刀具下架，并妥善保管。世博期间刀具销售定点单位将由市商务部门向社会公布。违反上述规定的，将由有关部门予以取缔，并予以处罚。<br />
<br />
安全管理措施规定，危险性刀具实行实名购买和销售登记制度。刀具销售单位应当查验购买人员的身份证件，登记购买人身份信息、购买刀具的品种和数量等情况。销售登记情况每周报所在地公安派出所备案。未履行查验登记备案义务的，由公安部门责令限期改正，并予处罚；情节严重的将由商务部门注销《世博期间刀具销售定点单位》证。<br />
<br />
对危险性刀具销售单位安全管理要求中，一是实行专店或者专柜销售，设专人负责安全管理；二是销售场所配备必要的安全技术防范设施；三是严禁向行为异常、精神异常人员和未成年人销售刀具；四是发现行为异常、精神异常人员购买刀具的，及时向公安机关报告。违反上述规定的，由公安部门责令限期改正，并处罚款；情节严重的将由商务部门注销《世博期间刀具销售定点单位》证。<br />
<br />
安全管理措施规定，严禁携带管制刀具、陶瓷刀进入公共场所或者乘坐公共交通工具，违反规定的将予以处罚。携带其他危险性刀具进入公共场所或者乘坐公共交通工具的，应当采取必要的防护措施，确保公共安全，未采取必要的防护措施的，将由公安部门责令改正，并予以处罚。<br />
<br />
有关部门要求，单位和个人应当妥善保管所持有的刀具，防止刀具被盗被抢。同时，鼓励市民积极举报非法销售、携带刀具的行为。</div>

							
							
							
							
