南韩组合「东方神起」<br />
<br />
南韩为报复北韩在今年3月击沉军舰「天安号」，早前计划打心战，透过在边境播放性感组合WonderGirls的热舞影片，来融化北韩士兵冰冷的心。心战尚未开打，但「韩流」已悄然攻陷北韩高干子弟的心，令他们疯狂迷上南韩乐队「少女时代」和「东方神起」，还破禁穿紧身裤和留长发，用南韩潮语秘密沟通，令当局大感头痛。<br />
<br />
北韩消息人士透露，「时下的青少年很喜欢南韩的流行音乐，不只迷上歌星的外表或歌唱技巧，还很陶醉于他们的舞姿」。「东方神起」、「少女时代」，北韩年轻人早已挂在嘴边，还暗地里交换影碟和复制音乐，「甚至模仿他们的衣着和甫士」。<br />
<br />
托亲友中国买老翻<br />
<br />
<img src="https://pic.chinatopsex.com/images/16wh3pnew.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
青春活泼的歌唱组合「少女时代」，受到北韩高干子弟追捧。互联网<br />
<br />
在北韩这个共产铁幕国家，民众的发型和衣服都有规定，但随着「韩流」杀入，青少年为追逐潮流而不惜破禁。消息人士说：「紧身裤和长发在部份青少年当中很流行，有男孩子让头发留长，盖过耳朵；女孩在公众假期时穿紧身裤外出。在假日，平壤常光街或乐园百货商店，总会看到几个这样打扮的青少年。」<br />
<br />
消息人士指，这股「韩流」并未成为一股疯魔全国的社会风气，而是局限在「平壤、南浦、平城、元山等大城高干子弟的专利，普通工人的子女，做梦也不敢这样做」。<br />
<br />
北韩是个甚么都管的极权国家，民众是透过购买中国盗版光盘和廉价中国制造的多媒体播放器，才能轻易接触南韩流行音乐。南韩流行音乐盗版CD，在中国每只只卖数元人民币；歌手演唱会DVD，也只是卖数十元人民币。高干子弟们每次得悉有亲友获准到中国，就会托他们买回来，然后互相交换这些违禁品，令南韩歌手的歌声，静静地传遍北韩。<br />
<br />
尽管当局禁止销售记忆卡和手提媒体播放器，但只要有钱，就可以在大城的黑市中买到。例如在平壤统一路场或火车站场，以13美元（101港元）就可买到中国制的2G MP4机。<br />
<br />
北韩极力对抗「韩流」入侵，国家安全局和公安已展开打击「长毛党」的行动，一旦发现衣着、发式不合乎规例者，就会罚款1,000北韩圜（5港元），但问题是「长毛党」和穿紧身裤的大多是高干子弟，公安在执法时始终有所顾虑，担心会得罪权贵。而且怎样判定一条裤紧身与否，也会有争拗。<br />
<br />
爱用南韩潮语沟通<br />
<br />
除了崇拜偶像，北韩年轻人更喜欢说南韩潮语。他们原本称好朋友做「同志」，但年轻人私底下改称为「朋友」；南韩女孩子称呼男性密友做哥哥的叫法，在北韩也很流行。不过他们只是私底下会说，每当外来人走过来，他们又会说回北韩正统语言，以免惹上麻烦。<br />
