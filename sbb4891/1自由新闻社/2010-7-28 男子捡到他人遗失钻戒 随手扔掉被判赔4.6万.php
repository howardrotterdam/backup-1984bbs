捡到钻戒又扔掉 男子被判赔4.6万 （北京晚报 2010-7-27 第10版）<br />
法院：捡到遗失物有保管义务&nbsp;&nbsp;<br />
&nbsp;&nbsp;本报讯（通讯员李桃 王要勤）录像显示一男子拾得他人遗失的钻戒，但该男子张某自称以为钻戒是假的随手扔了。失主王某提起诉讼，北京市第二中级法院近日终审判决指出，张某的行为违反了妥善保管遗失物的法定义务，他应该赔偿失主钻戒损失4.6万余元。<br />
&nbsp;&nbsp;去年7月9日，王某在本市丰台区一停车场将一枚价值4.6万余元的钻戒丢失。王某报警后，民警调取了停车场录像及银行录像，找到了拾得钻戒的张某。张某认可捡到钻戒，但自称当时认为戒指是假的，就随手扔掉了。王某起诉至一审法院，要求张某赔偿钻戒损失4.6万余元。张某辩称，自己没想到是别人丢失的，如果当时没扔，一定返还给王某。<br />
&nbsp;&nbsp;二中院经审理认为，拾得遗失物，应当返还给权利人。拾得人应当及时通知权利人领取，或者送交公安等有关部门。在此之前，拾得人应当妥善保管遗失物，因故意或者重大过失致使遗失物毁损、灭失的，应当承担民事责任。根据现有证据，可以认定张某拾到的戒指系王某购买的价值4.6万余元的钻戒，张某在拾到戒指后，未将戒指送交公安等有关部门，也未妥善保管，其自称将戒指扔掉，以致戒指灭失无法返还，该行为违反了妥善保管遗失物的法定义务，且具有主观故意，应对由此给王某造成的经济损失承担侵权的民事赔偿责任。</div>

							
							
							
							
