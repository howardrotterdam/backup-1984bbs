<br />
　　文章称，近来，美国海军航母“乔治-华盛顿”号及其他美国战舰到达韩国釜山港口，此举令中国很恼怒。<br />
<br />
　　此外，美韩联合军演“无畏精神”即将拉开序幕，这次军演让北京非常急躁不安。对此，北京大学国际关系教授、国际战略中心副主任朱峰表示：“此次军演所选择的时间非常微妙。北京政府担心这种情况会演变成与朝鲜的军事冲突，而这正是中国反对此次军演的原因。而且，这场联合军演正在使情况变得复杂。”<br />
<br />
　　文章指出，美国海军“乔治-华盛顿”号航母上次访问釜山是在2008年底，当时中国空中的海上侦察设备较少。不过，在中国发射新型卫星后，“乔治-华盛顿”号于7月初从日本横须贺基地出发，约两周后到达釜山的过程中都受到了严密监视。<br />
<br />
　　“无畏精神”演习开始之前，美国国防部长罗伯特-盖茨在首尔明确表示，虽然中国已经具备空中监视能力，但中国不会对演习决定产生影响。盖茨称：“这些演习是在韩国沿海举行，而不是在中国沿海。这场演习与我们数十年来所开展的演习是一样的。我们已在西海和东海进行了演习，所以根本没有什么会刺激到他们。所有人应该都很清楚，我们有意在这两片海域开展演习。”<br />
<br />
　　中国人民解放军海军不久将部署国产航母，但在此之前，中国必须决定建造哪种航母，而且，美国依靠庞大的超级航母及航母战斗群开展海上作战的模式，正好也适合中国。<br />
