													<h2>上海中学一校两制“隔离”民工子女</h2>
						
						
																											<div id="postmessage_674565" class="t_msgfont">　　（联合早报网讯）香港东方日报报道，有大量外地民工打工谋生的上海，有兼并了民工子女学校的公立中学，竟然划出“隔离带”，不仅把本地生及民工子女划分在不同区域上课，连校服、上下课和吃饭时间、师资等也不同，两区学生不准往来，违者会受罚。事件引起舆论关注，专家指会增加新一代隔膜和仇视，不利社会稳定。 <br />
<br />
　　上海前年起推行“关停并转”，逐一把农民工子女学校并入公立学校，以破除排外歧视，实现公平教育，惜事与愿违。有八百多名师生的江南学校，为宝山区最后一所关闭的民工子女学校，三百多名学生在今年寒假后被分流到同区一公立学校。 <br />
<br />
　　隔开两区 学生不准往来 <br />
<br />
　　该所公立学校随后出现“一国两制”的怪现象，民工子女学生被独立编派到西楼上堂，不与东楼的本地生一同上堂，东西楼各有一个篮球场和足球场，中间由停车场相隔，双方不能越界，如有东楼生在操场，西楼生要回避。 <br />
<br />
　　两类学生被标签化，西楼生要穿粉红、蓝白的新校服，以区别着蓝黑色校服的东楼生，他们的返学、午饭、放学时间全部错开，有意不让他们在校园相遇。同时，东楼生可用新椅、接受奖学金评比，并享有优先用场地设施、参观世博的“特权”。所有福利，西楼生统统无份，俨如二等公民。曾有西楼生令同学跌伤，校方强迫西楼生自费买保险。 <br />
<br />
　　教师唱衰民工子女搞分化 <br />
<br />
　　另外，教师们不断向东楼生灌输，西楼生“成绩差，又会抢钱”，另向西楼生强调本地生才是学校真正主人，“结交他们扰乱学校的秩序，是很危险的事情”。虽然有少数成绩优秀的民工子女可升级为东楼生，但也有阶级之分，他们考试的试卷要剔明是外地学生，不能参加上海学校的中考。 <br />
<br />
　　有专家指出，上海有近三十八万外来流动人口子女，以往只能在校舍破旧、师资匮乏的民工子女学校读书，但并校却未能根治歧视，深层次原因在于户籍制度。随着愈来愈多民工子女学校被兼并，情况会愈趋严重，增加新一代隔膜和仇视，不利社会稳定。</div>

							
							
							
							
