<br />
<img src="http://img1.cache.netease.com/cnews/2009/12/27/200912270524401eb88.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
南方网12月27日报道&nbsp;&nbsp;国家质检总局原局长李长江日前复出，担任全国“扫黄打非”工作小组专职副组长。<br />
<br />
三鹿事件：李长江负领导责任<br />
<br />
据中国江苏网、《新华日报》等报道，12月24日至25日，中共中央委员、全国“扫黄打非”工作小组专职副组长李长江赴江苏视察指导工作，肯定了江苏“扫黄打非”工作和打击手机网站淫秽色情信息专项行动成果。<br />
<br />
今年65岁的李长江生于1944年10月，黑龙江双城人;曾任团中央直属高校中国青年政治学院副院长、党委副书记等职;2001年任国家质量监督检验检疫总局局长，第十六、十七届中央委员。<br />
<br />
三鹿牌婴幼儿奶粉事件让世人震惊。2008年9月22日，新华社播发消息称，党中央国务院对奶粉事件责任人严肃处理：“根据国家处理奶粉事件领导小组事故调查组调查，三鹿牌婴幼儿奶粉事件是一起重大食品安全事件。鉴于在多家奶制品企业部分产品含有三聚氰胺的事件中，国家质量监督检验检疫总局监管缺失，对此，局长李长江同志负有领导责任，同意接受李长江同志引咎辞去国家质量监督检验检疫总局局长职务的请求。”<br />
<br />
专家：李长江复出无程序问题<br />
<br />
今年7月，中共中央办公厅、国务院办公厅发布的《关于实行党政领导干部问责的暂行规定》规定：引咎辞职、责令辞职、免职的党政领导干部，“一年内不得重新担任与其原任职务相当的领导职务”，“一年后如果重新担任与其原任职务相当的领导职务，除应当按照干部管理权限履行审批手续外，还应当征求上一级党委组织部门的意见。”<br />
<br />
对此，国家行政学院公共管理教研部教授汪玉凯昨晚在接受南方都市报记者采访时认为，李长江引咎辞职已满一年，担任新职符合上述规定：“李长江是正部级干部，属于中央管理;对他的问责和复出均需要政治局讨论决定，因此我不怀疑他复出的程序有问题。”<br />
<br />
“但问题是，像三鹿婴幼儿奶粉这样影响很大的事件，这样的官员复出以后会不会带来一些负面的影响?”汪玉凯强调，官员复出与否，复出后做什么工作，与他在公共事件中所承担责任大小、该公共事件对社会的影响力(尤其是负面影响)和造成的后果都是直接相关的。<br />
(本文来源：南方网 作者：陈宝成) 李蔚</div>

							
							
							
							
