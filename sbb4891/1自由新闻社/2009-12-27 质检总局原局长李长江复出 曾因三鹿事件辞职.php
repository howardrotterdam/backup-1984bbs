<br />
　　去年9月22日，鉴于在多家奶制品企业部分产品含有三聚氰胺的事件中，国家质量监督检验检疫总局监管缺失，对此，时任质检总局局长的李长江负有领导责任，国务院同意他引咎辞职。<br />
<br />
　　&gt;&gt;李长江简历<br />
<br />
　　李长江生于1944年10月，黑龙江省双城人，大学学历。历任团中央青运史研究室副主任、中国青年政治学院副院长、国家土地管理局副局长、浙江省副省长、海关总署副署长、国家出入境检验检疫局局长等职。2001年开始出任国家质量监督检验检疫总局局长。李长江是中共十六届、十七届中央委员。<br />
