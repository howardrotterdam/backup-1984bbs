《 京华时报 》（ 2009年12月28日&nbsp; &nbsp;第 007 版）<br />
　　本报综合消息因“奶粉”事件引咎辞职的原国家质检总局局长李长江日前已出任新职全国“扫黄打非”工作小组专职副组长。 <br />
