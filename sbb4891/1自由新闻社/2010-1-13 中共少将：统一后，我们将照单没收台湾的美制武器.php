<br />
自一九九○年代以来有关中国发展反导弹系统的报导和猜测就始终不断，这和科技带动的空权发展有密切关系。从一九九一年美国发动第一次波斯湾战争及其后几次战争的经验显示，在发动陆战以前，以攻陆巡弋导弹、弹道导弹、及各种精准空对地导弹的空中密集攻击以摧毁对手的指管通情系统，成为启动战争的模式。相对地，如何因应密集空中攻击的防空作为，成为军事领导人必须思考因应的工作。这是中国在北京及东南沿海部署严密防空系统的原因。<br />
<br />
空中攻击不只是短程导弹，尚包括中、长程导弹的传统精准攻击。美国小布希政府于二○○六年曾试图把洲际弹道飞弹的核弹头改为传统弹头，或特殊钻地弹头，以利于在极短时间内准确打击躲藏在美军兵力不及的偏远山区地下的恐怖份子。然而，这也可能成为远程精准攻击中国的利器。<br />
<br />
此外，周边的印度和北韩也致力于发展中、长程弹道飞弹。凡此，都引起中国的担心。如果，中国对美国对台军售“爱国者三型”做回应，其应该是显示导弹能穿透“爱国者三型”的防卫，而不是透露其试验了陆基中段反弹道飞弹的拦截技术。<br />
<br />
然而，从有限的媒体报导，我们很难评估中国发展陆基中段反弹道飞弹的真正进展。主要是中国没有说明其试验的条件。具体而言，中国没有说其拦截的方式是美国采取的撞击拦截，或采核爆炸拦截，因为前者的难度极高；中国没有明确说其试验是在外太空实际进行，或是在大气层内空中进行，甚或是电脑模拟进行，而只说是在 “境内”进行；其媒体没有报导是全战备试验，或只是发射及接战试验；报导也没有说明其试验是否包括拦截器的侦测诱饵能力等。<br />
<br />
总之，发展反弹道飞弹拦截技术是高难度的，必须长期投入大量资源，但是，不一定会有成果。因为，攻击方总能从质与量两方面发展出穿透的方式。同时，发展反弹道飞弹拦截技术的成本远比展弹道导弹高。这是为什么中国现在才发展反弹道飞弹拦截技术的原因，也是美国欧巴马政府停止反弹道导弹部署而集中于持续研发的原因。 <br />
