													<h2>政协常委徐展堂两会期间喝茅台中风</h2>
						
						
																											<div id="postmessage_503074" class="t_msgfont">(明报)2010年3月5日 星期五 05:10<br />
<br />
【明报专讯】有「红色资本家」之称的全国政协常委徐展堂，在北京出席人大政协两会期间突然脑中风昏迷。港区政协委员圈中盛传，现年69岁的徐展堂，出事前一天曾与公安部人员畅饮，其间4人喝光了两支酒精甚烈的茅台酒，但亦有说他前晚一早就寝，病倒未必与「劈酒」有关。徐展堂昨晨被发现昏迷，甚至一度传出他不治的消息，但官方消息强调，徐展堂昨晚进行3至4小时的手术抢救，现时情况危殆。<br />
<br />
港区全国政协昨日原定早上9时召开小组会议，但徐展堂久久未有露面。徐展堂在两会期间未有下榻港区全国政协委员入住的贵宾楼，而是入住他拥有的一所四合院。据了解，徐展堂的佣人昨晨约8时许在房外拍门，准备唤醒徐起床，出席政协小组会议。但拍门良久仍未见回应，入房查看，赫然发现徐展堂昏迷，毫无反应，立即把他送往协和医院抢救。<br />
<br />
佣人拍门无反应赫见昏迷<br />
<br />
大会立即通知徐展堂家人前往北京，据了解，院方待徐的家人抵京后，昨晚即为徐展堂动手术抢救，手术长达3至4小时，几近午夜才完成。徐展堂现时情况危殆，在深切治疗部（ICU）留医。<br />
<br />
徐展堂突然患急病的消息昨晚在港区人大政协圈子传出。据悉，徐展堂最钟情饮酒精浓度逾50%的「飘香国酒」茅台，更是香港国酒茅台之友协会的理事，两会期间已经常饮酒。出事前一天中午，徐展堂更与友人畅饮茅台，当时席上已有朋友劝他不要饮太多，当时他坚称身体没有问题。直至前晚，徐展堂再与公安部的友人在北京畅饮，4人共喝光了两瓶茅台，未知今次出事是否有关。<br />
<br />
不过，有徐展堂的友好透露，徐的家人指徐展堂前一晚在家中吃晚饭，未有外出，且一早就寝，相信今次中风与豪饮无关。据了解，徐展堂除有高血压，平日身体无大问题，友人更形容徐展堂平日千杯不醉。<br />
<br />
传曾畅饮 家人指早就寝<br />
<br />
昨晚消息传出后，身在北京的政协委员都担心不已，全国政协委员黄楚标形容，徐展堂平日为人仗义，对人热心，尤其提携后辈。<br />
<br />
在前天下午的全国政协大会开幕期间，记者看见坐在主席台上的徐展堂疲态尽现，频频「瞌眼b」，不时低头闭目养神。当日坐在主席台上有10多名港澳区常委，包括世贸中心协会副主席伍淑清、新鸿基地产非执行董事郭炳湘、新世界发展董事总经理郑家纯、四洲集团主席戴德丰及北海集团名誉主席徐展堂等。<br />
<br />
当日结着红黑间领带的徐展堂坐在主席台倒数第二排位置，其右为中国工程院院士、绕月探测工程总指挥栾恩杰，左为全国总工会副主席徐振寰。<br />
<br />
政协揭幕日不断打呵欠<br />
<br />
会议开始，政协主席贾庆林发表工作报告，可能由于讲话时间颇长，主席台上不少常委不断打呵欠及揉眼，疲态尽露。徐展堂在报告宣读开始时也是正襟危坐阅读桌上文件，后来疲累不堪，双手迭放在桌上，不时低下头「闭目养神」。</div>

							
							
							
							
