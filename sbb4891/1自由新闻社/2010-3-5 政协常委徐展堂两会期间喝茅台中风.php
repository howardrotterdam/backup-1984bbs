													<h2>拥私人博物馆　传涉军火交易</h2>
						
						
																											<div id="postmessage_504581" class="t_msgfont">Apple Daily 苹果日报网上版 －－2010-03-05<br />
拥私人博物馆　传涉军火交易<br />
<br />
 <br />
徐与妻子王爱梅 02年合照。<br />
<br />
【本报讯】北海集团名誉主席兼全国政协常委徐展堂，被形容为神秘富豪，少年时当银行信差、油漆工、云吞面档伙计等。 60年代投资地产赚得第一桶金，回归前几年更以不寻常的速度冒起；有传他介入中国与伊朗的军火交易而暴富。徐嗜好收集古董，并自设私人博物馆，藏品保守估计价值逾 10亿元，被誉为世界十大中国文物收藏家之一，可惜一场金融风暴令他重创。<br />
<br />
靠地产赚得第一桶金<br />
<br />
2000年徐展堂出席北宋宫廷艺术画展开幕礼，观赏他与法国前总理希拉克的油画。<br />
<br />
徐展堂 1941年出生，江苏宜兴人，生于江西吉安。 1950年他随家人走难来港。他 94年接受访问时表示，移居香港时一穷二白， 54年父亲因肺病去世，他与母亲、弟妹相依为命。他 13岁开始打工， 60年代起投资地产。<br />
<br />
67年暴动后，徐展堂见太子道住宅东方花园开卖，每个单位只要 2,000元定金，他一口气买了六个单位，后来香港经济复苏而赚了第一桶金。他的生意包括地产、制漆、酒店、能源等行业，以至中国电力、公路等基础工业。 80年代初，他先后收购香港老字号中华制漆厂、城巴集团和伦敦首都城巴、北海集团，成为跨国企业家。<br />
<br />
97年英国报章指徐介入中国与伊朗的军火交易。报章又称徐在伦敦不断变卖古董，套现 1.25亿，其欠债传闻由此开始。道亨银行、港基银行及中国银行分别向徐展堂名下的新中港集团、新中港贸易及银洲实业追讨逾亿欠款，其中港基银行更入禀要求法庭将新中港集团清盘，因此传出他出售城巴。<br />
<br />
爱好古董的徐展堂被称为世界十大中国文物收藏家之一。他曾斥巨资从苏富比、佳士得等拍卖会购回许多珍贵文物。经过 20年来的积累， 91年他成立本港第一家私人博物馆徐氏艺术馆。徐共收集 4,000多件价值高昂的古董，保守估计价值 10多亿元。</div>

							
							
							
							
