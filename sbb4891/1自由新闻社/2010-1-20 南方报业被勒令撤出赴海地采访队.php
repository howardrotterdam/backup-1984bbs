																											<div id="postmessage_443720" class="t_msgfont">制度稳固 建筑才稳固<br />
吴稼祥<br />
海地大地震牵动了国人的心，因遭遇相同而更心怀同情，因有同胞牺牲而深感悲痛。如果仔细想一想，更值得反思的，还是地震中的死亡人数，建筑物的倒塌状况，以及救援工作。虽然还没有确切统计，最保守的估计，在海地地震中死亡的人数，不会少于10万人。1906年，美国旧金山发生里氏8.6级（另一说是7.8级）大地震，官方公布的死亡人数为478人，也有人估计为3000人，最高估计为死亡6000人。1989年10月17日，美国旧金山再次发生大地震，震级为里氏6.9级，死亡超过270人。1994年1月17日凌晨，强震袭击美国洛杉矶，震级里氏6.6级，死亡62人。1995年1月17日（又是一个1月17日！）清晨，日本神户市发生里氏7.3级大地震，死亡5400余人。<br />
中国和海地的情况怎么样呢？发生于1976年7月28日凌晨的唐山大地震，震级里氏7.8级，遇难人数为24.2万。2008年5月12日下午，四川汶川发生里氏8.0级强震，死亡69，227人。这次海地地震死亡人数也至少在5万人以上。<br />
对比一下上面数据，不难发现，7次大地震，震级相当，死亡人数却相差甚远。发生在美国和日本的4次地震，没有一次死亡人数达到1万人，少则几百，多则数千；发生在中国和海地的3次地震，没有一次死亡人数少于6万人，特别是唐山大地震，更是达到骇人的24万人。<br />
是中国和海地的地震发生地人口密度更大吗？可能有这个因素，但不是唯一的因素。洛杉矶、旧金山之于美国，神户之于日本，相比于唐山和汶川之于中国，都是更大的城市。神户发生地震时，市区人口105万，唐山市区呢，1986年还不到96万人，按照唐山市80年代每年增加2万人左右的增长速度推算，1976年顶多80万人。如果计算地震死亡率，对比更为惊人：唐山为30%，神户为0.51%，洛杉矶市区人口为360万，94年大地震死亡率为0.00172%。<br />
相近震级的地震，造成相差甚远的死亡率，这显然不是天灾破坏性上的差别，而是受灾地人事上的差别。差别在哪儿？除了救援速度和技术以外，最主要的，是在建筑的抗震性上。看看这几次大地震的图像资料就知道，唐山、汶川和海地震后的建筑物大多呈粉碎性坍塌，被压在下面的人，即使不被砸死，也会被窒息死，使救援不是没有意义，就是增加难以克服的难度。在地震中粉碎性坍塌的建筑，很少是钢筋混凝土结构，大多是豆腐渣工程。<br />
经过几十年战乱，和后续的大跃进、文化大革命折腾的中国，与人斗其乐无穷，哪有心思把功夫花在建设上？文革中建的那些居民楼，单薄得像蛋壳，风都吹得倒，哪能抗震？改革开放后好多了，但相对贫穷的四川，花在学校、医院等公共设施上的钱本来就少，再加上缺乏权力制衡条件下，难以避免的公共工程中的权力寻租行为，必然导致偷工减料，也就是建筑物豆渣化。汶川地震中，学生成为死亡率最高的群体之一，原因或在于此。<br />
海地在光荣地夺取了两项国际锦标（第一个独立的拉丁美洲国家，第一个独立的黑人国家）之后，流血就开始代替流汗，武器就开始排斥工具（海地国旗和国徽上有一面战鼓，两把战斧，两门大炮，六枝带刺刀的步枪），从独立到联合国特派稳定团进驻，这个国家不是暴政，就是暴乱，几乎没有中间状态，既没有稳定的宪政民主，也没有稳定的专制独裁，有过皇帝，有过国王，但除了杜瓦利埃独裁29年外，其他的都很短命，长的几年，短的数月。从1804年到1915年间，上下台的统治者有90多位，几乎每年一位，这样的国家哪能有牢固的建筑？<br />
诺贝尔经济学奖获得者阿玛蒂亚.森发现，饥荒灾害的本质不是粮食匮乏，而是权利匮乏，民主体制可以预防饥荒。人类的任何体制都不可能预防地震，但地震可以检验出一个社会体制的优劣。体制越优的国家，地震的破坏性越小，次生灾害也越小。<br />
建筑与国家，都要求平衡性结构。海地是世界最贫穷的国家之一，2004年的人均GDP只有380美元，而与它同在一个岛上的多米尼加，2008年人均GDP高达4952美元。为什么有如此大的区别？原因之一，就是海地以极端种族主义观念立国，曾经对白人进行种族灭绝，以至今天，黑人占人口的95%。而同岛的多米尼加，黑白混血种人和印欧混血种人占人口的73%，白人占16%，黑人占11%。中国呢，曾经搞的是另一种极端主义，即极端阶级主义。就像一根木头支撑不起一栋建筑一样，独裁种族、独裁阶级和独裁权力也支撑不稳一个国家。<br />
简单的结论是，无论是国家，还是建筑，最具有抗震性能的结构，是既具有相互牵制和平衡，又具有一定弹性的结构。有稳固的国家，就有稳固的建筑。</div>

							
							
							
							
