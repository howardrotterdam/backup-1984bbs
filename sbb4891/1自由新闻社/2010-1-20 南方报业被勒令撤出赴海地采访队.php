救援遭遇“无政府状态”<br />
14日早6：00，王志强随民事警队出发到各居住点查看房屋，在废墟中找寻防弹衣、头盔。<br />
8：00，他去联海团后勤基地报到，签字（证明你安全，可以工作），以国别为单位上岗上街巡逻，维护治安秩序，告知民众水电正在抢修，援助食品会及时发放，在地震被困点实施营救。<br />
在机场待命的美国、法国、多米尼加救援队奔赴各现场。美国救援队已经在总部废墟下追踪到一名美国维和警察的生命信息，通过软管送液体食物。中国救援队也得到较大进展，确定方位坐标，定点搜寻。<br />
海地政府和联合国呼吁紧急援助海地后，全球各国纷纷向海地捐款和援救物品，派遣救护队。联合国人道主义事务协调办公室发言人拜尔斯称“来自世界43国的1739名搜救队和救护队在海地展开救助。”据悉，71名幸存者被救出。<br />
由于余震频繁，截至17日，等级在5级以上的余震已经发生14次，机场拥堵、港口瘫痪，救援设备缺油缺电等因素，救灾工作进展缓慢不断有新灾民出现。<br />
美国伯克利“海地紧急救援基金会”主席瓦尔特在博客中写道：“我们对海地救援速度之慢感到愤怒，很多人在等待医疗救助过程中死去。”<br />
地震发生后，瓦尔特和房屋主人一起在院子里搭建了临时医院，在接下来一天半的时间里，大量伤员涌入。瓦尔特不满地说，在临时医院工作的所有人中，没有一人有医学专业背景，“我们有大量过氧化物瓶子，还有一些洗碗用的肥皂，反正能用的都用上了。”<br />
联合国、援助国政府、世界银行和慈善机构需要面对如此棘手的无政府状态：<br />
总统府被夷为平地，旁边的警察局总部大楼倒塌了一半，里面空无一人。警官及其家人也同样需要帮助。总统府前的广场上，到处都是灾民们搭起的临时帐篷。广场上充满了排泄物的气味和尸体腐烂的臭味。他们用捡来的树枝生火做饭，用泥水洗衣服，晾在帐篷外面。<br />
基础设施能力崩溃，港口、医院、住宅、学校、道路的状况都很差，除了首都一处商业区外，几乎没有供水或供电。水泵和供水管道可能已经严重破坏，教师、医生、工程师和专业人士很多在地震中受伤或死亡。<br />
中国地震应急搜救中心总工程师曲国胜告诉南方周末记者，这次海地救援不同于巴基斯坦地震和四川汶川地震，海地政府瘫痪，国际援助机构几乎找不到当地政府的任何公共服务网络可以对接，2004年印度洋海啸之后，印尼亚齐省的政府设施至少还在，但在海地，其公共服务却已荡然无存。<br />
海地政府几乎没有自救能力，只能苦等外国救援。海地的批评者说，即使在地震前，这里也缺乏一个有效政府。为在第一时间实施救援，国际红十字会以及联合国的救援人员还在连夜将英文的应急救援手册翻译成当地人能读懂的克里奥尔语。<br />
《纽约时报》19日报道美军82空降师分发食物和水的场景。幸运的人们知道，食品发放区在这个高尔夫球场山顶。这里现在住着好几万人，来这里仍是个问题。老人有优先权，但一些老人仍然上不去。有小孩的家庭也有优先权，所以据说有人借来孩子，把他们抱上山。另一个失败是，士兵曾在得到了援助的人们手上盖戳，防止他们重复领取，把多余的食物卖往黑市而剥夺真正需要的人的机会，但人们把墨水洗掉。<br />
政府没有军队，警力严重不足，为避免大规模骚乱发生，海地政府高级官员卡罗勒・约瑟夫17日说，从即日起至1月末，海地将处于紧急状态，实行宵禁。这一措施旨在维护灾后太子港的社会治安。根据这一规定，当地时间下午6时以后，除警察和维和军人，所有人禁止上街，在6时22分以后，所有直升机不能起降。<br />
<br />
“无主之国”考验全球<br />
海地街路两边和广场上到处是一顶顶小帐篷。18日早7：30，王志强急急出发，半小时前警察总部行政管理局电话通知各国警队长：要召开总部全体维和警察大会。会上，王志强见到了联海团特别代表和新任警察总监。特别代表说，鉴于海地救援特别需要，已报请联合国安理会增派1500名维和人员赴海地执行维和任务。<br />
1月20日中午本报截稿前，王志强从海地发来最新消息：国际社会的救援物资杯水车薪，饥饿的灾民成群结队在街上游荡或到政府或联合国驻海地的组织机构门前等待，已发生至少两起灾民暴力哄抢救济物品事件。海地社会治安状况因灾情不同而各不相同，杰马克和太子港灾情较重，治安形势也十分严峻，随时有爆发大规模示威、游行或暴乱的可能。近日每天夜晚都能听见零星枪声，由于当地警察奉命不得向又饥又渴的民众开枪，他们只能向天空鸣枪，但这根本起不了阻吓作用，已有警察遭袭死亡的报告。<br />
美国军方负责救援工作的高级官员对媒体说，暴力事件已对救援工作和救援物资运输及发放造成影响。<br />
美国前、现任总统合作起来，决定展开历史上规模最大的救援活动。截至18日，美军已向海地派出超过1.1万名军事人员。原来曾统治海地百年的法国也积极出面展开救助，各国民间团体纷纷涌入海地首都太子港。和海地国民来自同一种族的非洲国家塞内加尔政府宣布：“我们已做好准备向海地国民提供土地。海地的老百姓们，只要你们愿意，就到塞内加尔来。”<br />
一名海地政府消息人士告诉法新社记者，海地18日起设立了280个紧急安置中心，为无家可归的灾民提供临时住地、发放救援物资。每个紧急安置中心大约有500个床位，计划设在首都太子港和周边6个城镇的学校、教堂等公共建筑内。联合国下属世界粮食计划署承担运营安置中心和调配重要救灾物资职责。<br />
但震后第6天，营救行动中不和谐音增高，时闻谴责和责备之声。据英国《泰晤士时报》报道，震后第6日，援助机构和国家谴责已接管海地机场的美国军队给自家飞机优先权。无国界医生说，美国军队还声称“优先权必须立即给运载救生设备和医疗人员的飞机”。 从上周二开始，着陆的40%都是军机。机场外，有援助和救援人员抗议当抢劫和违法现象在海地街头急速上升时，看来似乎没有人管。<br />
骚乱威胁、可怕的伤口、食物匮乏、栖身所缺乏，已是海地人的极大烦恼，但这些都无法与百万灾民正在经受的致命脱水可能性相比，特别是首都太子港。这座城市的供水系统已经瘫痪。<br />
国际红十字会朔尔诺先生告诉《南方周末》，他们为太子港3个居民聚集处供水，满足临时帐篷里7500个居民的用水需求，还建起了10个公厕，在各个临时帐篷里提供10个急救中心。<br />
200万人需要每天的食品和饮用水，但世界粮食计划署说，过去一周内只分发了25万份即食食品。一些饥渴难耐的灾民在街头或临时帐篷内焦急等待救援，还有一些人失去耐心开始哄抢救灾物资。首都太子港弥漫着绝望的气息。多个国际组织警告说，若救援物资不能尽快送达灾民手中，可能导致动乱，并且引发第二波疾病和死亡，爆发更广泛的人道灾难。<br />
无国界医生组织向本报记者形容重伤灾民数量前所未见，估计未来数日需进行约400宗截肢手术。<br />
蒙特里桑唯一一家还在运转的医院。大约有400名病人在临时诊所里候诊，但只有4名医生，临时诊所是用帆布和两个金属集装箱在后院临时搭建的。<br />
那里挤满了人，还有几十名伤病员在门口等候。朔尔诺先生告诉本报记者：“他们无力应付这种局面，过去2天有50多名患者丧生。”大约50名外籍医生将很快抵达太子港，但对一些重伤的幸存者而言，可能为时已晚。<br />
法国援外事务部长阿兰・约安德特呼吁联合国调查美国在援助中的主导角色。他抗议：“是在帮助海地，不是在占领海地。”在海地的援助人员抱怨联合国、美国和援助机构之间缺乏协调，当星期六机场关闭以便希拉里视察海地时，他们被激怒了。<br />
联合国秘书长潘基文17日抵达海地视察灾情并慰问灾民。他说，世界粮食计划署未来两周将向100万灾民提供食品，未来1个月向200万灾民提供食品。 他还前往海地总统府附近广场上一处临时居住地看望灾民，其中一群情绪激动的灾民喊道：“我们需要食物，我们需要帐篷，我们需要工作。”<br />
“协调与合作是这场人道主义救援的关键，尤其是救援保障体系，它决定了救援物资的运输效率。”联合国情报服务部驻日内瓦理事Corinne Momal-Vanian对南方周末记者说，“实质性援助需要持续下去，不仅仅是几天，几个月，而是连续数年。”<br />
地震后，之前每日要阻止至少3000海地人的多米尼加边境开始默许海地人越过边界。联合国难民署新闻发言人表示，美国与加拿大也表示暂停将非法入境的海地人遣送回国，鼓励其他国家向这些国家学习。人口不到1000万的海地，有300万人在国外，他国难民寄回国内的钱几乎成了最大收入来源。<br />
牛津大学经济学教授保罗・科利尔对媒体称，许多国家和援助组织已经提出海地主要重建方案，但是这些方案都缺乏协调性。各国现在需要的不是像开认捐大会一样只顾掏腰包，而是做出联合承诺，推动重建工作。<br />
对联合国的巨大考验是，在失去三名海地最高指挥官后，不仅要继续维持比震前严峻数倍的社会稳定局势，同时协调国际社会救灾，避免混乱局面，又要帮助已经失去国家主心骨的海地重建国家机器和继续民主进程。<br />
联合国发言人告诉南方周末记者，安理会定于18日开会，讨论协调对海地的国际救援行动。而联合国也正和美国讨论建立一个改进的协调机制。比如，联合国维和行动负责海地的街道安全，美国的军队协助满足人道主义需要。联合国还发出了5.6亿美元的人道主义紧急援助呼吁，这笔钱用于建立一个初步国际援助工作框架。<br />
加拿大外长劳伦斯・坎农说，商讨有关海地地震救灾和灾后重建事宜的国际会议将于本月25日在加拿大蒙特利尔召开。坎农表示，会议着重重新评估海地形势，并为海地的长期稳定和灾后重建作准备。<br />
达・席尔瓦指出，当务之急是做好计划，“当地社区有效参与是最成功的灾后反应，最好的办法不是依靠援助而是自我发展。”<br />
可海地是全球最依赖援助的国家之一，而且在处理援助资金方面的纪录不佳。<br />
联合国指出，腐败、政府能力低下、捐助者和政府之间的不信任都会阻碍重建工作。援助机构正越来越多地绕过政府，该国行政工作日益混乱。过去10年共有超过40亿美元投入该国的重建工作，然而，管理不善、缺乏协调以及国际机构将海地作为新自由主义经济试验品的做法导致重建努力一再失败。15亿美元的外债拖垮了海地的经济，政府入不敷出。更大的危险在于本应用于重建的资金被政府或个人挪用。<br />
不过，艾瑞克，现在纽约联合国总部工作，20世纪90年代担任超过四年联合国在海地的发言人，19日仍然乐观地对南方周末记者说，“我一直赞赏海地人的自尊和强韧，未来他们仍需要保持自尊和强韧。如果有一个国家能在地震后振作起来的话，那这个国家就是海地。”<br />
他解释说，多年来，虽然海地一直被各种弊端所困扰，但他们有一个民主选举产生的政府和一个正常运作的国家。话虽如此，很明显，联合国关注的问题之一是尽快恢复统治秩序治理机制，尤其是警察，维和部队必须提供支援，联海团已在机场附近帮助成立临时指挥部，开展联合行动。“所有的国际援助参与者，包括联合国、美国和其他参与成员，都是在援助海地政府。”潘基文18日意味深长地对全世界记者说，“他们有独立自主的优先权，我已经和海地总统讨论过，让他告诉我们在里这救援时，如何做才能保证不触范到他们的主权。”<br />
由于政府机构建筑在地震中严重受损，海地内阁会议17日被迫移师露天空地，一条长凳和一些塑料椅子便成为内阁官员们的坐席。会议只商讨震后重建和恢复社会秩序等问题,可以预见的是，计划中的2010总统大选将会无限期延期。<br />
“海地人总是把希望寄托在下一任总统。”曾在海地大选时持枪保卫投票点的一名中国维和警官说。<br />
（本报陈军吉、实习生陈晨、郭海燕对本文亦有贡献）<br />
