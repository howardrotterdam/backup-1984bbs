“我先以为车胎爆了，车横于路中央，烟尘扑面，道路就像汹涌的海浪一样左右摇摆，路边的围墙一片片倒塌，再以为是炸弹爆炸，接着看到一幢幢建筑倒塌，才醒悟：地震！地震！”联合国海地稳定特派团（简称联海团）警察总部刑事侦查局中国籍警官王志强在日记中写道。<br />
海地时间1月12日（北京时间1月13日）下午4：50，他刚下班出车辆配修基地，车子猛烈地摇晃了6－8次，差一点冲进路沟。王志强用摄像机拍摄了路边呼喊的人群，回基地，听到大厅有人呼救，他和同事跳下车，冲进大厅，余震中，房顶的水泥石块还在下落，将两个受伤的联合国工作人员抢出。手机不通，他想请安全警卫用对讲机联络医疗救助，却得到惊人信息：警察总部已经倒塌！<br />
地震发生时，清华同方公司的海地员工张振堂看到桌子突然从地面弹起来，到了胸部位置，地震发出巨大声响，就像战争爆发一样。想到一楼大门已上锁，就从卧室爬上阳台，头裹着一条随手从房间里抓到的毯子。他又看见书架、沙发和冰箱在房间里“跑来跑去”。大概过了一分钟，外面平静了。张振堂从三楼的阳台跳到二楼的一个水缸上，再下到一楼屋顶，慌乱中顺着一个消防梯下来。<br />
13日凌晨3点多，蹲坐在马路上的张振堂接到了公司从国内打来的电话，报了平安之后，他回到房间取了两个沙发垫和几件衣服出来在马路上过夜，“马路上都是神色慌张跑来跑去的人”。当天下午，外交部在海地的工作人员找到了张振堂和杨德光，开车把他们带到中国驻海地维和警察防暴队，安置在营区。<br />
1月19日晚，深圳中兴通讯公司11名驻海地员工乘坐国内安排的包机安全撤离到邻国多米尼加后，首次向外界讲述他们的惊魂一刻：<br />
“我们集体外出采购食物，正在回宿舍的山路上，一辆车前，一辆车后，山上发生了山体滑坡，第一辆回去的车安全通过，但第二辆车被滑坡的山地挡在半山腰，情况紧急，听到山下传来哭喊声，看到有人把伤员从山。上抬下来山体在继续滑坡，大家跑到空旷的地方，又一路小跑一个多小时才回到山顶的宿舍。”幸运的是，等他们跟国内打完两通电话报平安之后，海地的国际网络就断掉了。<br />
地震发生当晚，中兴员工们就计划好少运动，少洗澡，不敢在屋子里睡觉，在院子里搭起了临时帐篷，确定好每天晚上有人值班，两个人一班，每班三个小时。而很多海地人并不太了解地震的避震措施，有很多人继续在房子里睡觉。<br />
18日，宏远公司海地分公司经理王文杰告诉记者，地震后公司立即联系前方，但海地通信当天几乎全部瘫痪。最后，通过阿联酋微弱的信号联系上了。王文杰说，地震来时，5名员工正在二楼，到楼梯口发现墙体出现倾斜，5人赶紧跑回办公室，等到摇晃不那么剧烈后才沿着倾斜的墙体，从二楼下到了地面，最后与中国驻海地贸易发展办事处工作人员取得联系并被及时转移到了安全地带，18日这5名员工转而加入了中国国际救援队的医疗队伍中。<br />
<br />
未能逃离的“人间地狱”<br />
一片废墟中，被埋在联海团总部大楼的8名中国维和警察终于被发现，但已全部死亡。<br />
17日早8：00，四面联合国旗覆盖上了战友的灵柩，在参加完遇难战友的告别仪式后，王志强必须继续执行任务。<br />
当日他的任务是在联海团临时医院和阿根廷医院帮助医疗人员诊治灾民，同时核实死亡者身份并运送到指定地点。<br />
工作内容每天都不一样，王志强形容“刑事侦查局是救火队，哪种工作没人做，缺人做或做不好，他们就必须上，必须做好。”<br />
据王志强观察，联海团临时医院由两个大帐篷临时改建，每个有300平方米，每个帐篷放置行军床约100张，总计约200张，同一时间临时安置约200名伤者，但实际上至少有250名伤者在此同时救治，并由联海团和国际组织负责4万余人的饮食。<br />
伤者去世后来不及尸检，来不及标注尸体并按序按类存放。因此王志强除了需要帮助医生约束伤者保持镇定，协助其进行截肢或腐肉切除等手术外，还要协助法医简单尸检，清理尸体时，和同事紧密配合，一人标注尸体，一人持相机照相，一人持本记录。<br />
伤者在手术前后痛苦哀号，腐败滴水尸体的气味弥漫，面对或面目狰狞或无手无脚或头颈尽断、皮肤相连的尸体，王志强带着口罩，带好手套，穿上防护服，平静地工作。在死神随时可能伏击的海地，王志强已学会了冷静面对死亡幽灵的每一次角逐。去年12月5日意大利籍维和人员驾车驶离银行，即遭三名不明身份武装分子抢劫，身中数弹，惨死街头。<br />
震后第三天，中兴员工们从住处转移到防暴队。詹子娟感觉街上“已经很混乱了”，人们担心骚乱一触即发。<br />
有很多人在找食物、找油，街上挤满了车和人，平时步行5分钟路程要开2个半小时，他们不敢下车，外国人此时在海地到处走相当于自杀行为。太子港到处都见手持大刀的人，街头经常传来枪声，持械抢钱案经常发生，乘坐厚玻璃汽车才能安全一些，“喝得酩酊大醉的生还者睁着发红的眼睛互相斗殴，人们的目光从惊恐转变为绝望和愤怒。”<br />
“车上有维和部队的士兵带枪保护，暴民不敢攻击我们。”詹子娟说。在车上，她都可以闻到整个街上恶臭逼人，令人作呕。她透过玻璃看到沿路大部分的贫民房屋倒塌了，在一条街上，几十个成年人和青少年冲进一家倒塌的手机店，在混乱中推搡争夺。街上的广播里一直在用英语、法语、西班牙语三种语言提醒，“要把遇难者的尸体送到国家提供的两个固定地点烧掉。”<br />
詹子娟听到整个城市哭喊声不断，“广播里有人还不断地打电话进去呼救、求救”，大地每晃动一次，贫民窟里都会传出焦急且痛苦的候鸟啼鸣声，灰尘像乌云一般覆盖了整座城市。<br />
每当运载救助品的直升机飞过，人们便在飞扬的尘土中跟着跑，哪怕能拿到一瓶水。一瓶水就是生存希望。<br />
她还惊恐的看到，有人不知道跑到什么地方在哄抢什么东西。詹子娟很快又听说海地现在处理尸体的人缺乏，“人们非常担心接下来两三天尸体开始腐烂，瘟疫开始流行。”所幸3天后他们在祖国帮助下成功逃离“地狱”，“外国人能及时撤出的并不多。”<br />
而在市中心的战神广场上，悲剧日日上演，没能逃离的数千名幸存者住在太子港目前最大的临时营地中。数万名地震幸存者在太子港各街区的临时帐篷里度过了第5个夜晚。<br />
根据红十字国际委员会一线专家提供的消息，人们依然很难获得临时住所、厕所、水、食物和医疗服务。虽然城里能够买到某些食物，但价格飞涨，大多数人已买不起任何东西。<br />
国际红十字会太子港分会发言人西蒙・朔尔诺先生向南方周末记者描述了震后一周的最新情形：<br />
“黑色和白色的垃圾堆成了山，污水漫过街道，一些女人兜售脏兮兮的蔬菜，冒着黑烟的公共汽车里挤满了人。街上有很多肿胀腐烂的尸体，许多尸体还流出黄色液体，虽然摩托车和汽车在旁边来来去去，也没有人看一眼。年轻人从倒塌的建筑上把水泥块搬开，但他们不是在搜救幸存者，而是在找废铁。他们目前只关注自己的生存。有些人找到了一点阴凉地儿，但大多数人都坐在太阳底下。小便的腥臭味令人作呕。”<br />
朔尔诺说，39岁的马蒂娜正用一桶水给儿子洗澡，这桶水已经有好几家人用过。她的丈夫一早就出去打饮用水了，他们现在一无所有。<br />
“数万太子港居民生活在临时帐篷里，那里的卫生情况不容乐观。如果要避免流行疾病的暴发，这是首先要解决的问题。”朔尔诺对本报记者说。<br />
英国《每日邮报》报道，愤怒的群众杀死一名怀疑抢掠者后，捆绑尸体的双手双脚，再拖着尸体当街示众，一名男子更用木棍狂打尸体。<br />
在王志强警官的记忆里，其实海地以往就是私刑泛滥，“当场被民众发现的犯罪分子往往在警察到达现场前已经被处以私刑――集体打死。”<br />
有媒体报道，甚至连救援者也受到枪击。藏着魔鬼的瓶子似乎已被打开。<br />
“失败国家”的致命一击<br />
如果没有地震，中兴公司的员工们也不敢随意外出，“海地是很危险的，晚上我们不会在外面玩或者参观。”詹子娟说。<br />
震后他们就已买不到水和柴油，海地平时很少有市政供电，都是靠柴油发电，汽油也需要柴油作为燃料，“我们心里非常着急。”詹子娟说。这个地方一贯比较乱，她联想到2008年发生的动乱，那时也没水没电。更令人担忧的是，她听说海地出现“为了一杯水就杀人”的情况了，“如果当时邻居要抢劫我们就很危险，我们没有佩枪，海地的枪支非常泛滥。”<br />
美国《外交》季刊说，海地在世界上60个失败国家中排名第12位。国际透明组织认为海地是最腐败的国家，政府的软弱使毒品走私猖獗；警察不能控制本国匪帮暴力。<br />
海地历经多年的家族独裁统治、政治动荡、经济低迷是治安混乱的根本原因。1995年海地总统阿里斯蒂德解散军队后，20万枪支散落民间，或明或暗地依附于100个政治派别的非法武装活动依旧猖獗。抢劫、绑架、凶杀屡见不鲜，走私、偷渡成风。即使是海地首都太子港，也有太阳城、马提桑和加尔富尔三个贫民区长期被非法武装暗暗操控。<br />
联合国驻军最大的任务就是恢复当地的治安，15年来，海地政府宛如端坐在联合国军队费力托起的一块阿拉伯飞毯上治理国家，而飞毯内部并不是一片祥和之色，官员勾心斗角、腐败横行。<br />
维和警官王志强依然对2009年5月发生在太子港的游行示威记忆犹新，部分大学生为教师争取最低工资，在拉瓦拉斯党的资助下，游行演化为设置路障、投掷石块、砸毁商店、焚烧车辆、对抗军警，施压政府的暴力骚乱。<br />
海地是世界上贫富差距最悬殊的国家之一，10%的最贫困的居民只占总收入的0.7%，而10%最富有的人却占总收入的47.7%；80%的海地居民是穷人，每天收入不足1美元；75%的住房没有卫生设备；不到40%的居民能享用自来水；没有清除垃圾的服务；失业率高达80%，平均月工资不到50美元。<br />
联合国的统计数据显示，海地是世界上最贫穷最不发达的国家之一，有75%的国民都生活在赤贫状态下，全国只有20%的居民能用上自来水，成人文盲率高达80%。联合国世界粮食计划署每天向50多万海地学生提供一顿饭，而这顿饭往往是这些孩子每天唯一的一餐。<br />
2008年4月，海地民众在疾呼政府采取措施控制物价无果的情况下，愤然走上街头，“反饥饿、降粮价”的抗议游行迅速在各地掀起，首都太子港的抗议游行一度演化为骚乱，6人死亡，数百人受伤。<br />
政府未曾有效制订建筑标准，海地的房屋“看上去是用轻型建筑用砖或煤渣砖建造而成，而地震多发地区的建筑通常需要用金属连接砖块”，以避免砖块因晃动而引起建筑坍塌。<br />
美国科罗拉多大学地质学教授米莱蒂说，在人均每日收入只有两美元的海地，要想找出可以抵抗住7级地震的房屋几乎不可能，就连富丽堂皇的总统府以及联合国维和部队总部都在这场灾难中难逃一劫，成了一堆废墟，更别说平民百姓的栖身之所了。有报道称，海地60%的房屋都属于危险建筑。米莱蒂把海地建筑抗震性能不足归因于经济因素限制。他说，海地作为一个经济相对落后的国家，无法投入大量资金建设防震、抗震房屋。<br />
与海地相比，同样位于伊斯帕尼奥拉岛的邻国多米尼加也感受到了这次强烈地震的威力，不过由于国力状况明显好于海地而没有发生严重灾情，古巴东南部的房舍也随之晃动，但没有造成重大损坏。<br />
“如果你要列举西半球最多灾多难的国家，那一定是海地。”美国佛罗里达大学“降低灾难风险”项目组主任理查德・奥尔森教授说。2008年8月，风暴“费伊”、“汉娜”和飓风“古斯塔夫”、“艾克”接连袭击海地，洪水泛滥、山体滑坡，导致426人死亡、850多人失踪，100万民众无家可归，直接损失10亿美元。<br />
