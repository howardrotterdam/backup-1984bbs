													<h2>南都报道：地震预警两年前已拉响，劣质建筑却无力应对</h2>
						
						
																											<div id="postmessage_442520" class="t_msgfont">地震预警两年前已拉响，劣质建筑却无力应对<br />
<img src="http://pics.nddaily.com/d/94/d942d71d77b0fd96/Thumb/53935/dcga032001.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
摘要：遭受强震的海地，是位于复杂断裂带的岛国。世界上还有很多断裂尚未得到深入研究，尤其是位于发展中国家的断裂带，如亚洲、北非、南美，某些断层距离人口集中的大城市很近。城市规模越大，人口越集中，但在经济欠发达地区，相应的避灾设施和技术却并未随之普及。<br />
<br />
&nbsp; &nbsp; 遭受强震的海地，是位于复杂断裂带的岛国。世界上还有很多断裂尚未得到深入研究，尤其是位于发展中国家的断裂带，如亚洲、北非、南美，某些断层距离人口集中的大城市很近。城市规模越大，人口越集中，但在经济欠发达地区，相应的避灾设施和技术却并未随之普及。<br />
<br />
&nbsp; &nbsp; 本报记者采访了几位对海地地区有追踪研究的中美科学家，他们中的一些人曾在地震前建议当地政府加固房屋，但最终“来不及”缓解这场灾难。我们试图追问，海地之殇，会给邻近的岛国，给同样有复杂地震风险的中国带来什么启示。<br />
<br />
&nbsp; &nbsp; 海岛被断层包围<br />
<br />
&nbsp; &nbsp; 得克萨斯大学地质研究中心的保罗曼和他的研究伙伴们，可能是最近这10多年来，最关注海地断层的地质学家。他在邮件里告诉本报记者，从1980年毕业，自己就开始研究北美洲板块和加勒比板块交界区域的地质活动。<br />
<br />
&nbsp; &nbsp; 突如其来的海底地震，让保罗曼和他的研究伙伴们，突然成了媒体眼中的科学明星。之前，他们埋头苦心钻研北美-加勒比板块之间的断层，其中就包括海地地震所在的恩里基洛-芭蕉园(E nriquillo-PlaintainG arden)断层。这条断层早已为人知晓，但在1995年，才由保罗曼画出它的细节图。<br />
<br />
&nbsp; &nbsp; 芭蕉园断层有500多公里长，沿东西方向穿过海地、向东到达多米尼加共和国，向西到达牙买加。上周突发的7级地震，就沿这条断层展开；美国地质调查局公布的数字显示，其震中离海地首都太子港25公里，震源距地面13公里。<br />
<br />
&nbsp; &nbsp; 芭蕉园断层所在的区域，是一个非常复杂的地质活动带，布满了火山、地震，甚至偶然会发生海啸。关于这次地震更多的细节，仍然有待研究。<br />
<br />
&nbsp; &nbsp; 加勒比板块约有320万平方公里，它与北美洲板块、南美洲板块交界处，都是地震频发区。美国伍兹霍尔海洋研究所资深研究员林间，对位于芭蕉园断层北部的大断裂Septentrional有过深入研究，他说，“生活在加勒比板块和北美洲板块交接处的居民，比如海地、牙买加、波多黎各、多米尼亚，会发现自己被许多断层包围”，因为在这个地区，除了大的板块南北美洲，还有很多细小破碎的板块。<br />
<br />
&nbsp; &nbsp; 频繁的地震史<br />
<br />
&nbsp; &nbsp; 海地所在的伊斯帕尼奥拉岛，向来是地震高风险区。《海地岛的大地震记录》，发表于《美国地震学会通报》，作者谢勒详细记录了这个岛屿频繁的地震史。<br />
<br />
&nbsp; &nbsp; 伊斯帕尼奥拉岛并不是远古断层构造的产物，它形成于晚三纪，至今，包围它的断层仍在活动，现在，它由东部的多米尼加和西部的海地组成，位于古巴东面90公里的海域。<br />
<br />
&nbsp; &nbsp; 伊斯帕尼奥拉岛，史上又称西班牙岛、圣哥明岛，哥伦布最先发现它，16世纪时，岛上已经有15座城市。西班牙人带来的天花，令土著人很快绝迹，西班牙岛随即成为加勒比海盗的据点。17世纪末，这座小岛被西班牙割让给法国。直到1791年，黑人起义，1804年，他们在这座岛屿的西部成立海地共和国。<br />
<br />
&nbsp; &nbsp; 在谢勒的统计中，海地的地震几乎从未间断。1701年，中心城市Leogane就遭到地震重创，石头房屋全部被摧毁，沿海道路沉入海底。当时太子港还不是城市，在这次地震后，才取而代之成为中心。但在1751年10月，太子港突然发生了两次强烈的地震，持续了3分钟，所有的房屋轰然倒塌―――当时这座城市还只有100多栋房子，其中3/4为石头建筑。1个月后，震动又一次来袭，从地下传来的巨大噪音就如大炮轰鸣，灾情雪上加霜。<br />
<br />
&nbsp; &nbsp; 1751年的地震，让太子港的管理者思考，是否该用木材重建城市，但这个计划最终被放弃，石材被用于重新砌筑城市，10年后已经有683所房子屹立在首都。<br />
<br />
&nbsp; &nbsp; 1770 年，海地的圣灵降临节，地震幽灵重现，顿挫的噪音，从东到西碾过西班牙岛。当太阳再次从浓烟中探出头，这座城市变得非常可怕，大地张开了上千个口子，医院和监狱变成废墟，政府大楼、议会大楼、新建的教堂荡然无存，只有成排的树木能够帮助户外逃生的人们辨认街道的痕迹。所幸的是，大部分居民在第一次震动后撤出户外，4分钟后，第二次震动才发生，但仍有100多位居民不幸丧生。而相对于当时的城市人口规模，这个数字已经非常庞大。<br />
<br />
&nbsp; &nbsp; 1751 年后，吸取地震教训，政府曾短暂鼓励用木头建房，并且限制砖石结构房屋的建造，要求即使用石料，房屋内部也要用木板支撑。然而两三个世纪以来，城市急剧扩张，全国900万人口有20%居住在太子港，加之内乱不断，在拥挤而混乱的首都里，人们并无心修筑稳固的房屋。<br />
<br />
&nbsp; &nbsp; 海地的房屋质量受诟病已久。据纽约时报报道，美国一位慈善组织的董事曾访问海地，称“几乎所有建筑都存在重大缺陷”。来自纳什维尔一位设计师的调查表明，海地大多数建筑都是由混凝土建造的，但由于混凝土和钢材昂贵，一些承包商暗地在水泥中添加沙土，甚至很少采用钢材。另外，海地没有统一的建筑标准，建筑物在设计时极少考虑安全因素。<br />
<br />
&nbsp; &nbsp; 曾建议政府加固房屋<br />
<br />
&nbsp; &nbsp; 正因为如此，在观测到海地有7级地震威胁时，几位地质学家马上告知海地总理，建议加固房屋。<br />
<br />
&nbsp; &nbsp; 长期观测Septentrional断层和芭蕉园断层的保罗曼，以及来自美国普渡 大学地球和空间科学系的教授卡莱斯，告诉本报记者，他们和其他合作的科学家，在过去的几年里，发现海地周边的断裂形变正在加速。<br />
<br />
&nbsp; &nbsp; 根据观测数据，他们发表了一系列的学术论文，都是讨论这个区域的形变压力，及孕育地震的可能。卡莱斯在北美-加勒比板块安装了G PS仪器，可以观察到板块运动的形变。对多米尼加G PS数据的收集，显示这一区域地质风险增加，位移数据告诉地质学家，Septentrional大断裂，以及太子港附近的芭蕉园断裂，都可能是潜在的地震区域。<br />
<br />
&nbsp; &nbsp; 在2008年举行的加勒比地质大会上，保罗曼、卡莱斯和合作者们共同发表了一篇文章，提出海地及邻近地区可能会发生7.2级地震。但这个预测并未提及具体的时间和地点，卡莱斯告诉记者，“我们确实做不到这一点，也不能说清地震的级别，我们能做到的，只是说明这里聚集了可以发生7.2级地震的能量”。<br />
<br />
&nbsp; &nbsp; 2008年，太子港区域的地震风险，由卡莱斯和他的合作者――― 来自海地矿业能源局的Claude Prepetit，告知海地总理。几位科学家希望政府能够加固房屋，特别是学校、医院等公共建筑。卡莱斯对本报记者说，“我们确实这样建议了。海地官员也表示，非常理解和接纳我们的报告，但不幸的是，可以采取行动的时间实在太少了”。<br />
<br />
&nbsp; &nbsp; 来不及采取行动，直到上周7级地震发生。而太子港地区的上一次地震灾害，发生在1770年，就是圣灵降临节的那次，距今已有两百多年。<br />
<br />
&nbsp; &nbsp; 大地震风险未解除<br />
<br />
&nbsp; &nbsp; 海地地震再次证明，决定地震破坏力的，不仅是地震本身，还包括房屋质量。和麻省理工有地质研究合作项目的地质科学家林间说，历史上很多地震都强过海地，虽然海地震源较浅，震中距离太子港较近，但糟糕的房屋质量，也是带来大规模伤亡的重要原因。<br />
<br />
&nbsp; &nbsp; 虽然海地地震的这段芭蕉园断层，应力已经释放，或许100年内不会遇到另一场同样或者更高震级的地震。但科学家认为，这条断层其余部分，以及加勒比地区庞大的断层网络肯定是“活动的”。<br />
<br />
&nbsp; &nbsp; 林间说，在地震后，许多人往往很快忘却，“但是如果你的住宅距离一条活动的断层很近，你最好不要忘记你住在什么地方”。<br />
<br />
&nbsp; &nbsp; 已经发生的海地地震，可能并未完全化解这个区域的地质风险。另一条更危险的大断裂Septentrional，已经潜伏800年，被地质学家们认为仍具威胁。它位于芭蕉园断裂北面，在海地和多米尼加共和国北侧沿东西方向运行。这是历史上孕育过8级地震的地方，1946年，一场8.1级的地震――― 比海地地震强30多倍――― 袭击了伊斯帕尼奥拉岛的东北角。<br />
<br />
&nbsp; &nbsp; 预警了海地牙买加地震风险的地质学家保罗曼告诉本报记者，在这条断裂带上，多米尼加的Cibao流域，也许会发生最高可达7.5级的地震，在过去800年里，北边的断层都没有活动了，而它受到的形变压力比芭蕉园断层大得多。<br />
<br />
&nbsp; &nbsp; 但他说，“我们不能得知时间、地点、震级，我们唯一能说的是，手中的数据告诉我们，Cibao流域是不久的将来，很可能会发生毁灭性地震的地方”。而北加勒比板块国家都可能会遭到大断裂Septentrional的威胁，包括波多黎各、牙买加、古巴、开曼群岛、洪都拉斯以及危地马拉。<br />
<br />
&nbsp; &nbsp; “非常危险”，保罗曼告诉本报，上述国家人口密集，距离断层又是如此之近。所以，人们必须接受地震知识的良好培训，即使他们在一生中都可能没有机会经历一次大地震。<br />
<br />
&nbsp; &nbsp; 很多断层不为人知<br />
<br />
&nbsp; &nbsp; 事实上，国际地质学界对于避灾害的认识是统一的：需要一个稳固的“金三角”，断层的地质研究、提高修筑牢固房屋的技术、全面的公众教育，任何一个角都不能缺少。<br />
<br />
&nbsp; &nbsp; 世纪之交，国际社会曾经有一个预测，认为21世纪因天灾死亡的人数，将比20世纪锐减，但从2004年印尼地震、2008年汶川地震、2010年海地地震看，第一个10年，自然灾害仍然在困扰我们。中国地震局一位高层学者这样告诉记者。<br />
<br />
&nbsp; &nbsp; 这位学者说，目前还很难对比，在两次大地震中，汶川和太子港，哪一个受到的破坏力更大。地震的破坏力不仅和震源深浅、震中距离有关，还和破裂方向有关。汶川地震的破裂带长达300公里，就像一头趴在地下的动物突然苏醒了。<br />
<br />
&nbsp; &nbsp; 另一位科学家说，根据有限的数据推测，太子港多数房屋的建筑质量也许比汶川更差。<br />
<br />
&nbsp; &nbsp; 另一个可以对比的是，同为岛国，日本的地震可能比海地更多。但日本已经积累了丰富的抗震经验，保证把生命和财产损失降低到最少。但要让海地照搬日本经验恐怕比较困难，两者的社会发展水平相距太悬殊。<br />
<br />
&nbsp; &nbsp; 潜伏地震危机的断层，在经济发达国家，往往能得到更深入的研究。和芭蕉园断层同为走滑断层的，是美国加利福尼亚州的圣安德鲁斯断层。这条断层位于北美大陆板块和太平洋板块之间，科学家已经在这里打了深达3.8公里的井进行研究。<br />
<br />
&nbsp; &nbsp; 而芭蕉园断层之前得到的关注和研究都比较少，林间说，世界上仍有很多断层，我们所知甚少。特别是在发展中国家，包括亚洲，伊朗，北非，南美，一些断层距离人口集中的大城市很近，将来或许还有更多相似的灾难突如其来地袭来。<br />
<br />
&nbsp; &nbsp; “即使没有坚固的房屋，至少要保证你有一张结实的桌子”，这位地质学家说，当地震到来时，“迅速钻到桌子底下”。<br />
<br />
&nbsp; &nbsp; □采写：本报记者　杨传敏</div>

							
							
							
							
