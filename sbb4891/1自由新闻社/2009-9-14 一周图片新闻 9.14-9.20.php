													<h2>坚守讲台的山村教师们</h2>
						
						
																											<div id="postmessage_240053" class="t_msgfont">.<br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
.<br />
<br />
<br />
<br />
<br />
<br />
<br />
.<br />
<br />
<br />
　　<img src="http://img3.cache.netease.com/photo/0001/2009-09-10/5IRUKCBF00AN0001.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　9月8日，伍良明和冯家琴夫妇与学生们在一起。龙滩乡保洞村村小是四川省广安市广安区最偏僻的村小，伍良明和冯家琴夫妇是这所“夫妻学校”仅有的两名老师。<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://img4.cache.netease.com/photo/0001/2009-09-10/5IRUK8JT00AN0001.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　9月8日，保洞村村小简陋的教具。这所村小的校舍也十分简陋：一排三间平房，两间是三个年级合用的教室，一间是伍良明夫妇的住房兼办公室。<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://img3.cache.netease.com/photo/0001/2009-09-10/5IRUKBAI00AN0001.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　9月8日，伍良明和冯家琴夫妇在简陋的厨房里做晚饭。就是在这样艰苦的条件下，伍良明1988年底被调到保洞村后，一干就是21年，如今已经59岁。1993年，他还说服妻子放弃生意，来到这里当临时代课老师。<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://img4.cache.netease.com/photo/0001/2009-09-10/5IRUK9TB00AN0001.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　9月8日，伍良明在给五年级学生上语文课。<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://img4.cache.netease.com/photo/0001/2009-09-10/5IRUKCUL00AN0001.JPG" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　9月8日，在安徽省巢湖市居巢区散兵镇董山小学，邵军老师为孩子们上课。今年42岁的邵军老师，19岁时因意外事故失去右臂。身体残疾后，他自学获得大专文凭，于1998年被分配到董山小学任教，11年间全心投入到教学中。由于近年来董山小学高年级学生到镇上学校读书，今年开学，董山小学只剩下由新招到的8名一年级学生组成的一个班级。<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://img3.cache.netease.com/photo/0001/2009-09-10/5IRUKDH300AN0001.JPG" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　9月8日，安徽省巢湖市居巢区散兵镇董山小学的邵军老师护送学生们放学回家。<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://img4.cache.netease.com/photo/0001/2009-09-10/5IRUKFC600AN0001.JPG" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　9月9日，山村代课老师刘彦木送学生们回家。江西省德安县吴山乡大岭村地处偏僻，是为数不多的省级贫困村。村里每年都有20多个孩子要读小学，教学点却留不住老师。刘彦木主动找到村干部，要求担任代课老师。他说，看到那么多孩子没人管，我于心不忍。于是他成了九江地区唯一的一名代课老师。大岭村教学点开设了学前班、一年级和二年级，所有的课程都由刘彦木一人承担。<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://img4.cache.netease.com/photo/0001/2009-09-10/5IRUKEND00AN0001.JPG" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　9月9日，刘彦木老师在课间和孩子们做游戏。<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://img3.cache.netease.com/photo/0001/2009-09-10/5IRUKI2A00AN0001.JPG" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　唐权友在给孩子们上课。安徽长丰县杜集乡迎新村上杜小学，是长丰县最偏远的小学，自1998年作为该乡中心村的巴张小学的教学点以来，现年57岁的唐权友，一直是这所学校唯一的老师。他担当了校长、各科老师、校工、后勤等各种角色。为了支持唐权友的工作，连他的妻子都成为学校不拿工资的“编外人员”。十余年来，从上杜小学走出了数百名孩子，而年近六旬的唐权友老师仍然在坚守。<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://img4.cache.netease.com/photo/0001/2009-09-10/5IRUKG1B00AN0001.JPG" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　唐权友和妻子高国珍在学校操场前的菜地里翻土。<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://img4.cache.netease.com/photo/0001/2009-09-10/5IRUKMKR00AN0001.JPG" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　9月7日，江西省德兴市黄柏乡油榨村小学残疾教师徐合合在课余时间和学生们做游戏。年近半百的女教师徐合合因患小儿麻痹症落下残疾，她自18岁开始任教以来，一直扎根在山区的偏远小学，目前她的学校里仅有不到30名学生。徐合合三十年如一日，每天步行近10里山路到校上课，从她手里送走了一批批成材的学子。在她教过的学生里出过博士、硕士，她自己也连续20多年被各级部门授予优秀教师、优秀班主任等荣誉称号。<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://img4.cache.netease.com/photo/0001/2009-09-10/5IRUKM1200AN0001.JPG" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　江西省德兴市黄柏乡油榨村小学残疾教师徐合合兼任学校的音乐课教师。<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://img4.cache.netease.com/photo/0001/2009-09-10/5IRUKJC300AN0001.JPG" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　9月8日，孩子们用手语说出“谢谢”，向老师致以节日的问候。当日，在江西南昌市启音学校里，老师使用专业麦克风给聋哑孩子们上了一堂生动的语文课，孩子们戴着先进的助听器，在教师的耐心指引下练习发音、朗读课文，在爱心的交流中体验学习的快乐。</div>

							
							
							
							
