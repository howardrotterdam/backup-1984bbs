													<h2>高盛董事胡祖六：罢工也不能给中国农民工涨工资</h2>
						
						
																											<div id="postmessage_710999" class="t_msgfont"><img src="http://udn.com/NEWS/MEDIA/5692643-2367085.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
高盛顾问董事胡祖六表示，现在大陆各地的劳工罢工潮，对中国竞争力的负面影响最大，远远超过人民币汇率改革或升值的冲击。他指出，中国应以希腊为鉴，不能纵容劳资紧张关系的趋向，更不能煽风点火。<br />
<br />
人民币汇率近日再度受到关注，胡祖六接受大陆财经杂志专访时表示，汇率短期内大幅升值，在其他条件不变情况下，短时间内对出口企业竞争力会有一定影响。但决定长期竞争力的不是汇率，而是劳动力工本结构和生产效率的优化。<br />
<br />
胡祖六指出，如果本身竞争力不够，光靠人为压低汇率，难以为继，「没有靠廉价货币变成出口大国的」。中国变成出口大国也不是靠廉价货币，而是改革开放和生产效率不断的提升。<br />
<br />
胡祖六说，中国所以成为世界车间，吸引这么多外资、台资到中国，有三个因素：第一，中国有一批非常有纪律、受过良好教育、有素质又低工本的劳工队伍；第二，有比较好的基础设施；第三，潜在的、辽阔的国内市场。这三个因素结合起来使中国成为出口大国。<br />
<br />
当前，中国在农村还有很多富余劳动力，是个充分就业不足的国家，现在劳资关系变得比较紧张，罢工潮不断，是非常令人担忧的趋势。<br />
<br />
胡祖六呼吁，大陆官方、社会、包含全民、学术界，还是要关注让更多人要有就业。他指出，有就业才能消灭贫困。在这么多人还未找到任务的情况下，把工资提高，会让更多人不能就业。<br />
<br />
未来年，像鸿海、本田这样的企业，还能否像过去年那样，在中国有大规模的投资，将产业重心设在中国，胡祖六持保留态度。<br />
<br />
胡祖六透露，大陆近期出现罢工潮后，根据他接触的跨国公司所了解，一些跨国公司未来投资重点可能更多考虑到印度、越南、印尼，而不会像过去那样，自然而然就想到中国。<br />
<br />
他指出，如果外资不来中国，不来参与中国产业的提升，带来技术和新的产品，中国怎么会有竞争力？人民币再便宜，中国产品也出口不了。<br />
<br />
胡祖六强调，中国经济的未来及全球竞争力提升的关键，是要有和谐的劳资关系，因此不能通过政府短期政策来提高劳工工本。中国要避免经商环境和投资环境出现不利的变化，这些才是关系经济基本面的根本问题，汇率问题与之相比，无足轻重。 <br />
<br />
