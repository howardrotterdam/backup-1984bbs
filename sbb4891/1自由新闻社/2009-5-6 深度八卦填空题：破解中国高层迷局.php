2009年05月05日 字体大小：&nbsp;&nbsp;　&nbsp;&nbsp;<br />
去年下半年以来，中共云南省委组织部长辛桂梓遭人举报：&quot;身为中国共产党组织部门高级领导干部，知法犯法，利用职权贪污受贿，情节隐蔽、恶劣&quot;，&quot;还利用工作之便，到处散布所谓的内部消息、泄露共产党机密以及影射共产党领导干部。&quot;<br />
2008年4月出任中共云南省委组织部部长的辛桂梓，是一位自认非凡的人物。这位曲阜师范大学历史系毕业的山东人，当过前中共中央组织部长张全景的秘书，又历任中央组织部干部监督局副局长、局长，掌握了大量高层组织人事机密。<br />
<br />
揭发辛桂梓的材料，除送报中央领导人外，也被传到网上。<br />
<br />
去年下半年以来，辛桂梓却遭人举报：<br />
<br />
&quot;身为中国共产党组织部门高级领导干部的辛桂梓，知法犯法，利用职权贪污受贿，情节隐蔽、恶劣&quot;；&quot;还利用工作之便，到处散布所谓的内部消息、泄露共产党机密以及影射共产党领导干部。&quot;<br />
<br />
对辛桂梓的揭发，除了送报中央领导人外，也被传到网上。从材料内容看，举报者当是很熟悉辛桂梓的，披露辛所议论的人物也都指名道姓。以下是举报材料摘录，隐去原文中某些人的真实名字。<br />
<br />
山东ＸＸ设备有限公司董事长兼总经理ＸＸＸ是辛桂梓及其家属的&quot;后勤部长&quot;。据知情者反映，ＸＸＸ多年花费在辛桂梓身上的费用已有上千万之巨，辛的妹夫现为莱阳市委组织部部长。ＸＸＸ也相应得到了辛的支持并获得回报。此事只要调查ＸＸＸ即可明了。<br />
<br />
原山东省委副书记（现为省政协副主席）王ＸＸ及其秘书ＸＸＸ，每年都&quot;进贡&quot;给辛桂梓几十万的现金与购物卡。<br />
<br />
原山东省烟台市副市长矫智仁（已因受贿罪被双开），曾先后行贿上百万元给辛桂梓。当时正是通过辛桂梓的运作，矫智仁才当上山东省供销社主任（正厅级）一职的。<br />
<br />
讲述中组部内部如何尔虞我诈<br />
<br />
时任浙江省省长的万学远，打电话给江泽民，对中央研究的浙江省委书记人选表示不同意见，由于该情况只有江、胡锦涛和时任中组部部长的张全景知情，江异常震怒，下令严查何以万学远知道了？后查出是张部长的秘书安ＸＸ泄密，安被调离中组部，辛桂梓接替安，任张部长秘书，从而接触了大量的中央与地方组织人事机密。<br />
<br />
张全景任中组部部长时，武连元、李铁林和张柏林都是副部长，当时担任中办主任的曾庆红，极力推荐原香港工委秘书长孙晓群（与曾的关系特殊）任中组部秘书长，但武连元等三位部长坚决反对，张部长无奈之余，只好给曾庆红回话说通不过。没想到，后来这三人都给曾庆红表态，讲我们都同意，只有张不同意。此事直接导致张部长落选十五大中央委员，成为共产党高层的一大笑话。<br />
<br />
评论多位中央政要<br />
<br />
辛桂梓对多位知情人说过，ＸＸＸ是大野心家、政治流氓，想当皇帝。<br />
<br />
在孙晓群给当时的石油部部长当秘书期间，ＸＸＸ在国家计委也当秘书。据辛桂梓讲，ＸＸＸ在国家计委工作时，就非常风流，把某打字员的肚子搞大了，其老婆大闹离婚，他被计委主任开除，后来通过孙晓群调到石油系统工作。<br />
<br />
辛桂梓在多个场合还说过，ＸＸＸ任中组部部长期间，就行为不正，到处拉帮结派。在他的支持下，儿子与北京ＸＸ广逃邢薰司的总裁联手控制了全中国（尤其是中央电视台）的媒体广桃担牵涉利润达数十亿人民币之巨。据辛桂梓透露，ＸＸ曾一次就给ＸＸＸ送过几十万现金。<br />
<br />
辛还说，别看ＸＸＸ外表谨慎从事，实际上胆子大得很。<br />
<br />
对赵洪祝怨气深<br />
<br />
据辛透露，他本人与ＸＸＸ（现为中组部干部Ｘ局局长）关系最为紧密，经常在一起讨论有关官场运作事宜。<br />
<br />
辛桂梓曾对Ｘ局长说，我在中组部资历这么深，为什么在中组部老得不到提拔？反而要调往云南，原因完全在于赵洪祝种下的祸根。<br />
<br />
辛说，赵在中组部任副部长期间，分管干部监督局，对于收到的许多与中组部有关干部的上绦偶，赵都是让他以干部监督局的名义行文上报给部里，但赵收到报毯螅反而将报逃峙给有关被反映人，客观上造成了中组部的各级领导对辛的极大反感。<br />
<br />
陈良宇下台的真实原因<br />
<br />
辛桂梓透露，时任政治局常委的黄菊与江泽民的红人陈良宇是势不两立的上海派人物，对于如何处置陈良宇，黄与胡锦涛达成过君子协定，黄说，江泽民的工作我去做。黄找到江泽民说，良宇的问题非常严重，并说可能胡在参加上合组织会议期间会向江说情，从轻处理陈，黄对江说，您千万别上胡的当。后来胡果然找了江，征求江对良宇问题的处理意见，江在良宇问题的报躺现甘&quot;黄生活糜烂。作风腐败、一定要严惩&quot;。<br />
<br />
辛说，江一手培养的陈良宇的下台，是江泽民偏听偏信的结果。事后，江和曾庆红都非常后悔，并直接导致后来对黄菊秘书的严查。<br />
<br />
李ＸＸ与中组部的恩怨<br />
<br />
ＸＸＸ对中组部意见很大，为什么？据辛桂梓讲，在他担任文化部副部长期间，胡锦涛曾提议中央党校副校长去天津任副书记、刘奇葆去广西任副书记、他去江苏任副书记，但该提议被曾庆红控制的中组部压了一年多，一直未上会，造成李对中组部积怨很深。<br />
<br />
辛桂梓补充说，此人升任中组部部长后，曾公开在中央党校青干班上说过&quot;不要把中组部当回事&quot;之类的话，并采用在中组部外公开招聘局级干部的做法。辛还说，他不就是个团干部出身吗，有什么水平？这样与中组部现有势力做对，一定会自食其恶果的。<br />
<br />
辛对中组部一位副部长的评价是，他也是典型的政客，油滑、好色，情妇一大堆。此人最早在中组部干部四局工作时，负责联系经济科技部门，与周永康关系不错，经周等人推荐，去了四川当组织部部长，后又回到中组部任地方局局长、部务委员，当时曾经与现任人事部部长的尹蔚民一起竞争人事部副部长。<br />
<br />
辛披露，ＸＸＸ也是个道德败坏之辈，早在他担任吉林某地区书记时，就与一位电视播音员发生不正当关系。后调往民政部，在去西藏救灾时，与时任西藏自治区区委书记的胡锦涛拉上关系，后是胡提议将其调往浙江任省委书记。<br />
