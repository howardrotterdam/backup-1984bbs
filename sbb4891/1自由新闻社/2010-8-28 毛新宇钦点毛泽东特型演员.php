◎毛主席历史资料照片（上图）与红心剧照（下图）<br />
<br />
少将毛新宇力荐特型农民演员出演《开国》毛泽东<br />
<br />
红心“震惊”主席家人<br />
<br />
邵华建议：把主席演好，把企业的事放一放<br />
<br />
随着建党90周年的临近，一些相关影视作品接连上马，有关领袖扮演者的话题也渐渐受到各大媒体的关注，其中以毛泽东为最。从当年的古月，到后来的唐国强，再到不久前传出的电影《建党伟业》的青年毛泽东扮演者刘烨，到底谁更像、谁演得更好始终是人们津津乐道的话题。<br />
<br />
然而，近日，毛泽东的孙子――少将毛新宇却给国家广电总局电影电视剧重大革命历史题材领导小组发去电文，力荐特型演员红心出演即将开拍的建党90周年献礼片――电视剧《开国》中毛泽东一角。<br />
<br />
红心――到底何许人也？<br />
<br />
■毛新宇：红心重现主席风采<br />
<br />
由张玉中导演执导的30集革命历史题材电视连续剧《开国》开拍在即，但剧中最重要的毛泽东主席一角的演员人选迟迟未定，这令投资方、导演组乃至对该片关注的人们不免为此担忧，为建党90周年献礼的电视剧《开国》到底能否正常开机成了所有人的疑问。<br />
<br />
正当选角扑朔迷离之际，毛泽东主席的孙子少将毛新宇在8月1日建军节之际，给国家广电总局电影电视剧重大革命历史题材领导小组发去电文，力荐特型演员红心出演为建党90周年献礼的电视剧《开国》中毛泽东一角。<br />
<br />
毛新宇在电文中写道：“我作为毛泽东主席的孙子，多年来观看了由特型演员红心出演毛泽东的重大革命历史题材电影《毛泽东回韶山》、《毛泽东和他的卫士》、《天安门》、《情怀》后，我认为特型演员红心演得很真实，形似神似兼备，很有感情，重现当年主席的影子，最为适合出演建国前后时期的毛泽东形象，特推荐红心出演重大革命历史题材电视剧《开国》剧中的毛泽东一角色。”毛新宇对红心给予极高的评价，力荐红心出演《开国》中毛泽东主席的角色。<br />
<br />
红心到底何许人也？能让毛家人如此认可？记者带着很多疑问采访到了红心本人。 <br />
<br />
■形象震惊主席家人<br />
<br />
8月23日下午，在位于双井附近的一家咖啡厅，记者见到了红心。第一眼见到他，吓了一大跳，不管从身高、体重、肤色还是发型，没有化一点儿妆的他像极了毛泽东主席。<br />
<br />
从农民到特型演员，这就是红心人生经历的简要脉络。和许多人一样，从青年到中年，红心的人生职业和角色发生了多重转换，但特殊的是，他与众不同的相貌和气质，让他走上了一条独特的道路：毛泽东特型演员的演艺之路。<br />
<br />
红心1962年出生在哈尔滨市巴彦县一个普通的农民家庭。在扮演毛泽东之前，红心的生活和娱乐圈毫不沾边。年轻时，红心参过军、从过商，直到著名化妆师王海燕发现红心是个演毛泽东的“好苗子”时，红心才走上演艺道路。<br />
<br />
2004年12月26日，就在毛主席诞辰日的当天，红心意外打开了自己的从艺道路。<br />
<br />
“现在想想确实很巧合，那一天，我要为自己的酒厂找人来拍广告代言，但问了很多演员，他们要价都很高，我就突发奇想不如自己出镜吧，因为在这之前很多人说我长得黑，像包公，广告形象用包公卖酒也挺好，我就请了化妆师给我化妆。”红心回忆道。<br />
<br />
化妆师王海燕特别熟悉古月等扮演毛泽东的特型演员，看到红心那一刻，她惊喜地说：“你可以演毛泽东！”<br />
<br />
红心开始并没在意，只当是玩笑话。而王海燕却非常自信自己的判断力，坚持找来曾给特型演员古月化妆的资深化妆师宋世平，给红心做一个毛泽东的造型。疲惫不堪的红心在化妆师的摆弄中睡着了，两个半小时之后，等他醒来时看到镜子中的人，惊讶异常：自己和毛泽东还真有几分形似之处。<br />
<br />
在宋世平的建议下，红心拜见了电影学院表演系资深教授王淑琰和毛泽东研究院的院长应明阳。初次相见，王教授对红心的第一反应就是：太像了！太像毛泽东了！但形似是不够的，当演员必须要有表演的天分和智慧。王教授当即给红心出题，让他表演。毫无表演经验的红心竟然凭着自己对生活的理解和悟性，获得了王教授的赞赏。<br />
<br />
“王老师说我天生就是当演员的料，说发现我晚了，她给我七天考虑时间，让我想想要不要当演员，如果想从艺她就带我见毛主席的儿媳邵华阿姨。”红心回到家后，把这件事跟父母说了，当时母亲说了三句话：“好啊！如果真能让你演主席，就把企业包给别人做。演主席比做企业好。”但父亲当时就站起来反驳：“我反对，如果放弃企业，人家不让你演主席怎么办？”红心的爱人也特别反对，她说：“你就做梦吧，中国有十三亿人口呢，这跟你有什么关系，你怎么能演毛主席啊？！”而且红心的朋友同学也都不看好他。“我压力特别大，但是我天生胆子大，我觉得这是一件非常有意义的事情，值得试试。”<br />
<br />
到了第三天，红心决定还是去见邵华。“她见了我第一面也惊了，说我条件不错，真的很像主席，她说如果我想把主席演好，首先要把企业的事放一放，她说‘你个人多挣点，少挣点是小事，如果你能把毛主席演好是为全国人民服务’。”红心说邵华的这两句话当时触动了他，他一夜没睡，最后决定弃商从艺，走上特型演员的道路。<br />
