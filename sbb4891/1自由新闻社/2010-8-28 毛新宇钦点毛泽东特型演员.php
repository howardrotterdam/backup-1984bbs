◎特型演员红心生活照 <br />
<br />
■东北汉子苦练湖南话<br />
<br />
人到中年，有家有业，却要走表演道路，困难之多不难想象。他给自己树立了目标：学湖南普通话，学唱歌，学形体，学语言，学表演技巧……2005年9月，红心开始了每天前往电影学院的长途跋涉：早上5点起床，赶在交通高峰前出门。家人只是默默地看着忙碌奔波的红心，暗暗地为他的劳累心疼。红心是班里的老大哥，同班的同学们跟他差了有20多岁，但他却十分尊重同学。他深知自己表演学习刚刚起步，每当有同学说你可以演毛泽东时，红心总是不好意思地笑笑。在学校里，他跟同学们演小品、写短剧。王教授找来学生，扮成毛泽东身边的各种人物，专门给红心配戏。<br />
<br />
为了准确把握毛泽东的形体动作和说话方式，红心找来60多部有关毛泽东的纪录片，模仿、学习毛泽东的言行举止：走路、坐姿、吸烟、握手、鼓掌等姿态。为了保持与毛泽东相似的身材，红心甚至开始注意饮食，保持95公斤左右的体重。红心认真阅读学习《史记》、《三国》、《水浒》等毛泽东喜欢的名著，以拓宽视野，深入了解毛泽东的内心世界。<br />
<br />
学习毛主席的一切生活习惯并不难，难就难在是否学到精髓，要神似形像并不是一件容易的事。在大量观摩毛主席的纪录片后，细心的红心总结了很多细节的东西并加以运用，并深入生活走近毛主席身边的警卫员们了解主席的生活，他还特地为记者举了一个例子：毛主席在抽烟的时候，打火柴是滑的动作而不是敲击，灭火柴不是吹灭而是甩了两三下再扔掉。“很多时候，你要了解熟悉这些东西你才能钻进他的内心世界。”红心颇有感触地说。<br />
<br />
红心在扮演毛泽东的过程中，遇到的最大困难就是湖南话。特型演员红心是土生土长的地道东北汉子，用他自己的话说舌头不会打卷，而主席是湖南人，这就对演员在拍摄时的口型和同期录音的要求很高，红心坦言学湖南话与学绕口令不相上下。为了练习好湖南话，尤其是毛主席带有独特韵味的湖南话，红心特地请了3位湖南的语言老师，不仅如此，红心还在工作室安装了一面大镜子，每天对着它模仿纪录片里毛主席的一言一行、一举一动。采访中红心的语言老师小李向我们透露，红心为学习湖南话，多次把舌头都咬破了，以至于一段时间里只能喝粥。红心表示饰演毛泽东是他最荣幸的事情，没有什么苦是不能吃的。据了解为了体验生活，红心特意让老乡按照当时的生活条件做了糠米饭，睡的是木板床，这些都是为了更好地体会那个战争年代的生活，更好地诠释伟人。<br />
<br />
由于红心在刻画领袖神韵方面的独到功力，加上他外形与上世纪五六十年代的毛泽东酷似，红心接连主演了四部影片：《毛泽东回韶山》、《毛泽东和他的卫士》、《天安门》以及刚刚拍摄完成的《情怀》。<br />
<br />
到底什么样的特型演员才算合格？<br />
<br />
红心对记者说：“现在的特型演员和十年前、二十年前不同，现在只要你演员牌儿大，稍微在形象上化一化就能演主席。以前你要做一个特型演员有三个条件：第一要长得像；第二要演得好；第三要说领导的家乡话，具备这三个条件是优秀的特型演员。像唐国强他的戏演得好，我是长得像，在我心里最认可的是古月老师，他三个条件都具备，只可惜他已经离世了。”<br />
<br />
当记者问及红心是否有信心超越古月老师时，红心谦虚地说：“不能说超越，而是我要赶上古月老师，我也相信一定能赶上。戏要演好才是真本事。”<br />
<br />
“我会通过自己的努力打造好第二代特型演员。我的艺术人生永远是开始。”特型演员红心如是说。<br />
