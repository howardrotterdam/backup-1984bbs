据1月14日出版的《环球时报》报道　1月13日，谷歌高级副总裁、公司发展和首席法律官德拉蒙德在谷歌官方博客上发表文章威胁退出中国市场，并表示“不愿继续按照中国政府的要求对谷歌中国的搜索结果进行审查”。谷歌罕见地表现出强硬的公开对抗姿态，以前从来没有哪家跨国公司这样做过，路透社称谷歌在中国走上一条“玩战争边缘政策游戏”的道路。<br />
<br />
谷歌威胁退出中国市场的消息一出，<font color="red">中国互联网上13日出现激烈辩论，要求谷歌“滚得越远越好”和呼吁谷歌留下的网民都大有人在。13日晚，有人对环球网的一项调查发动干扰，致使“赞成中国政府接受谷歌条件”的投票数出现不正常急升，在遭到干扰前，反对和赞成中国政府让步的投票者分别占7成和3成。中国信息化与信息安全专家俞晓秋在接受《环球时报》记者采访时认为，虽然这一问题可能非常复杂，但谷歌不应该用外媒所说的类似摊牌的方式“威胁”中国，这不是一家商业公司所应当进行的赌博。</font><br />
<br />
《环球时报》记者13日下午来到位于清华大学东门附近的谷歌中国总部办公大楼，当记者提出采访要求时立即被守候在谷歌公司大楼门前的保安拒绝。随后《环球时报》记者多次致电谷歌中国公司总机，但一直无法接通。在大楼外，记者遇到了美国《洛杉矶时报》、香港《大公报》、香港无线电视台等媒体的记者，他们的采访要求也一样遭到拒绝。<br />
<br />
在谷歌大楼外，记者还遇到一些闻讯后前来的中国网民。清华大学一名计算机专业的杨姓大学生向《环球时报》记者表示，他是听说这一新闻之后专程骑自行车来到谷歌大楼附近打听消息的。他说：“我很担心谷歌退出中国市场会对像我这样的用户带来很大影响，我非常担心Gmail因此会用不了。但如果谷歌公司想要逃避审查，我无法接受。”<br />
<br />
一名不愿意透露姓名的前谷歌中国雇员13日对《环球时报》说，如果谷歌退出中国，将使广大互联网用户失去一个便捷快速的搜索引擎。<br />
<br />
不过，在IT业工作的一名姓张的先生认为，从网民的角度来说，少谁都一样过，不会特别在意，因为没有谷歌会有其他替代产品。如果谷歌退出中国市场，对中国互联网的影响并不会像西方媒体所说那么大。<br />
