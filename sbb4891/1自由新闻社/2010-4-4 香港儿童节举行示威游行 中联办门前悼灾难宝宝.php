<strong>天理何在？<br />
还赵连海及所有受害者正义<br />
救救中国灾难宝宝免于人为灾祸</strong><br />
<br />
2010年4月4日<br />
<br />
「地震宝宝、结石宝宝、血铅宝宝、疫苗宝宝、艾滋宝宝等等灾难宝宝，这些让我们国人羞愧的称谓，难道不让我们有所思索以及灵魂最深处的触动吗？一个孩子们经常受伤害的社会我们难道不惭愧羞耻吗？我们还能让在曾经发生的一切伤害继续在孩子们的身上再发生吗？」这是毒奶受害家长赵连海的呼吁。<br />
赵连海的儿子是三聚氰氨毒奶粉的受害者。三聚氰氨毒奶粉，已令到数以十万计的幼儿肾结石，中国官方并称有6名小孩死亡。但至今，政府当局不仅没有对受害的儿童作出妥善合理的治疗和赔偿，亦没有真正追究与毒奶粉事件有关官员的责任，国家质检总局局长李长江轻易地便可再复出，继而持续骚扰和严厉打压为保护子女勇于维权的家长。究竟天理何在？<br />
<br />
「结石宝宝之家」的发起人赵连海只是尽上作为父亲及公民的责任，为使其它儿童不再受到同样的伤害，透过宪法赋予的权利，为中国的儿童及在北京上访期间被强奸的安徽女子李蕊蕊讨回公道，却反被控「寻衅滋事罪」。明明是受害者却反成了被告。在3月30日案件开庭时，赵连海的亲属更被拒绝进入法庭旁听及见面。据其律师所讲，赵连海被戴上手铐和脚镣出庭，后在律师的抗议下，手铐才被取下，但脚镣一直戴着五个多小时。我们对政府当局这种颠倒黑白是非、违反伦理常识和侵犯人权的行为，表达强烈的抗议，并促请当局立即无条件释放赵连海和停止无法无天的违宪滥捕严打其它被捕的维权人士。<br />
<br />
以上只是其中一事例。所谓「虎毒不吃儿」，中共政府常说儿童是国家的花朵、社会未来的主人翁。然而，仅在这几年间，已发生多宗儿童受戕害的事件【见附件一，这只是冰山一角】。可怜这些无辜的幼童，就这样以他们的生命和一生的健康，成了黑恶权贵、贪污腐败、缺乏监督问责的强暴政权的牺牲品。中国历来血的教训已经太多，正如赵连海所说「万不能再让幼小童真的孩子们受到任何人为的伤害了」。这些无辜的幼弱受害者的惨痛悲剧理应深刻反思痛心疾首，每一个受难儿童的名字理应受到尊重铭记，以警惕国人避免人祸重演和珍惜生而为人的尊严权利。借着今天（4月4日）是香港的儿童节，我们呼吁公众关注中国的灾难宝宝，并声援那些为了这些宝宝抗争维权而惹上牢狱之灾的良心犯。我们并在此呼吁中国政府：<br />
<br />
还赵连海及所有受害者正义，立即释放赵连海及所有维权人士；<br />
为所有受政府人为灾害影响的儿童提供免费和妥善的医治，并作出合理的赔偿；<br />
正视所有人为灾难事件背后的问题，为中国儿童建立一个安全、健康的成长环境；<br />
落实《儿童权利公约》等国际人权公约，保障中国儿童的基本权益。<br />
<br />
【附件一】<br />
<br />
救救连绵天灾人祸下受害的灾难宝宝<br />
<br />
（1）大头宝宝<br />
<br />
2004年期间，安徽阜阳爆发劣质奶粉案，导致过百名农村婴儿营养不良、四肢短小、头部肿大，被称为「大头娃娃」。据报，阜阳市合共发现171件案例，其中13名「大头娃娃」死亡。紧接着在山东、广州、安徽等地也出现类似事件。多名官员曾被开除、撤职或责令辞职，但有些后来再复出。<br />
<br />
（2）爱滋宝宝<br />
<br />
九十年代初，国内农村兴起了「卖血奔小康」的浪潮，近百万农民在卖血过程中，不幸染上艾滋病，遗下至少14万的「爱滋孤儿」。在重灾区河南，有地方官员为了赚钱，不理卫生情况，盲目推动农民卖血。当事件爆发后，又企图隐瞒真相，打压医学专家和记者调查访问。高耀洁医生为了使世界知道真相，在2009年8月被迫出走美国。<br />
<br />
（3）四川地震宝宝<br />
<br />
2008年四川大地震曝露了豆腐渣工程问题，令近万名学生惨死。民间透过公民调查（截至2010年3月）搜集到5212个受难学生的名字。政府曾声言追究豆腐渣工程问题，但两年过去，政府当局不仅否认人命伤亡与豆腐渣工程有关，还阻挠家长公开悼念、压制追查真相及争取人道赔偿的罹难学生家长和维权人士。黄琦和谭作人因此分别被判监禁3年和5年。<br />
<br />
（4）血铅宝宝<br />
<br />
政府当局为了增加投资，盲目引进高污染和高耗能的项目，甚至不惜牺牲人民的生命健康和生态环境，包庇和纵容污染的企业。仅2009年以来，就有陕西凤翔、湖南嘉禾、湖南武冈、云南昆明、福建上杭等地，相继发生儿童铅超标和中毒事件，有人估计影响人数达到一万多人。据报，铅中毒可令小孩发育迟缓、食欲不振、行走不便与便秘、失眠；若是小学生，还伴有听觉障碍、注意力不集中、智力低下等后遗症。<br />
<br />
（5）污染宝宝<br />
<br />
每年因室内装修污染致死的中国儿童有210万，主要是因为室内装修中的甲醛和苯含量严重超标，除导致儿童上呼吸道疾病致死，也可引起新生儿体质降低、染色体异常、鼻咽癌、白血病、哮喘病、新生儿先天性异常、心脏病及智力降低。除此之外，空气污染及吸二手烟的毒害亦是令中国儿童长期处于人为污染的环境空间积累毒素及病发夭折。<br />
<br />
（6）结石宝宝<br />
<br />
由于农药的滥用、土地污染及有毒食品的制造，估计中国因食品污染导致五岁以下的儿童死亡有200万。2008年的三聚氰胺毒奶粉引发中国众多幼儿肾结石。中国卫生部2008年12月指出，全国因毒奶粉导致泌尿系统出现异常的儿童达29万余人，其中至少6名小孩因食用问题奶粉死亡。温家宝在2010年2月则指，受影响儿童多达三千万。各地争取赔偿和医治的家长一直受到严密监控，维权律师的工作亦受阻碍。发起维权的父亲赵连海在2009年11月被当局以「寻衅滋事罪」拘控，2010年3月30日开庭受审。<br />
<br />
（7）疫苗宝宝<br />
<br />
