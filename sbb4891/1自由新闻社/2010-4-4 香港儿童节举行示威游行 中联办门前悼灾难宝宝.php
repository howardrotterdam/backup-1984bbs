<br />
1994年12月8日，新疆克拉玛依市教育局官僚为欢迎“义务教育与扫盲评估验收团”的25位官员，组织全市最漂亮的能歌善舞的中小学生796人在友谊馆剧场举办「专场文艺演出」。<br />
<br />
因舞台纱幕太靠近光柱灯被烤燃而引起火灾。当燃烧的火团不断地从舞台上空掉下时，克拉玛依市教育局的官员出来叫学生们：「大家都坐下，不要动！让领导先走！」<br />
<br />
学生们很听话，都坐在自己的位子上不动；等上级政府与教育局所有在场的26个官员都从第―排撤退到最后一排的出口处「先走」了之后，教师才开始组织学生撤离，但此时电灯已全灭，大火已蔓延到剧场四周，唯一的逃生之路已被熊熊火焰堵住。<br />
<br />
于是，796名来自全市15所中小学的师生全部陷入火海之中，323人死亡，132人烧伤致残（注，另有一说：死325人，伤136人；此处采用法院判决书的数字）；死者中有288人是天真美丽可爱的中小学生。<br />
<br />
在场的有40多名教师，有36位遇难，绝大部分为掩护学生而殉职。<br />
<br />
在场的克拉玛依市副处级以上官员有20几个，当时他们的位置离火源最近，离逃生门最远，竟「奇迹般」地无―人伤亡，而且走出剧场门口时还个个衣冠楚楚。<br />
