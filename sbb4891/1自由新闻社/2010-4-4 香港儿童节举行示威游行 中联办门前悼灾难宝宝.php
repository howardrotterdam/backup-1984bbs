													<h2>大头宝宝</h2>
						
						
																											<div id="postmessage_548115" class="t_msgfont">大头宝宝<br />
<br />
毒奶粉<br />
自2003年5月至2004年4月，安徽阜阳爆出惊人的假奶粉案，过百名农村婴儿因进食劣质奶粉而导致营养不良、四肢短小、头部肿大，被称为「大头娃娃」。 阜阳市合共发现171件案例，其中有13名「大头娃娃」不幸死亡。据诊断，这些婴儿所患的都是营养不良综合症，而扼杀这些幼小生命的「元凶」，正是蛋白质 等营养元素指针严重低于政府标准的劣质婴儿奶粉。总理温家宝当时特别批示，要求国家食品药品监督管理局调查事件。紧接着在山东、广州、安徽六安等地也发现类似案件。<br />
<br />
2004年6月9日，监察部通报了对阜阳劣质奶粉事件9名责任人的处理情况。除了时任阜阳市市长刘庆强受到行政记大过的处分外，另有数名官员被开除、撤职或责令辞职。此外，安徽省质监局也对阜阳多名质监系统官员予以严肃处理。<br />
<br />
然而到了今日，当年的被问责的官员，多数重获要职。首批被问责辞退的城东工商所市管员陈春生和宋影，在上诉后获重任原职。被撤职的原阜阳市工商局分管市场监管的副局长杨伟，于08年已调往安徽省的一个地级市工商局担任副局长，重新回到被撤职之前的位置。被责令辞职的原阜阳市卫生局分管食品卫生的副局长丁丽玲，后转调阜阳另一家市直机关任副调研员，2007年下半年被任命为该局副局长。原阜阳市市长刘庆强在被问责后相隔一年多时间，调任安徽省环保局担任局长。<br />
<br />
人命关天的一件事，就如此轻易的被当权者忽悠过去。失职者连官也不用丢，更W论负上法律责任了。<br />
