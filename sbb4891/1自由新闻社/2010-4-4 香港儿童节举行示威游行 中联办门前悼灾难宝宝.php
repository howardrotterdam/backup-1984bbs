																											<div id="postmessage_548114" class="t_msgfont">爱滋宝宝<br />
<br />
血祸<br />
<br />
河南省首创「内靠公章、外靠血浆」的发财致富「新思路」，结果使河南省成为全国艾滋病增长最快的大省。一些地方官员明知道收集血浆的过程不合卫生，艾滋病已经肆虐其中，仍为了保住官位，一再压制民间声音，将艾滋病疫情说成「国家机密」，并不断发出采血许可证，人为造成了艾滋病的蔓延。「卖血─艾滋病─腐败─冷漠」结合在一起，将无辜的人们送上一条死亡之路。成群成村的农民感染艾滋病不治，遗下大量的「爱滋孤儿」。许多年仅六七岁的小孩，由于父母感染艾滋病，直接经由母体祸延下一代，还未出生便已注定要饱受歧视病苦，一辈子过着没有未来的生活。更为可怕的是，当年河南农民的「爱滋血」，已流向全国各地。血液和血液制品普遍被污染，上海、北京、安徽、河南、河北和湖北等地均发现同类事件。<br />
<br />
一九九二年，河南省卫生厅厅长刘全喜上台不久，就在一次卫生系统内部上讲，要大力发展「第三产业」，大办血站。河南有九千多万人口，百分之八十以上是农民，即七千多万农民。即使只有百分之一至百分之三的人愿意卖血，平均每年卖一至两次，将这些血液收集起来，买给生物制品公司，就能创造上亿元。「血头」们只顾自己赚钱，将同样血型不同个人的血液全都混合在一 起，用分离机分离，只收集卖血者的血浆，其余输回卖血者体内，造成艾滋病、乙肝和丙肝等疾病大规模流行。<br />
<br />
河南地区因输血问题，大规模感染艾滋病的时间约在一九九四年下半年至一九九六年。经过一段潜伏期后，感染者在二000年左右开始发病，大量死亡，才被广泛注意。从一九九一年至一九九五年，估计最少约有三百万人参加卖血活动。医务人员去年在某些「爱滋村」的调查发现，当年曾经卖血的农民中，有百分之四十至六十感染了艾滋病毒。<br />
<br />
然而，河南「艾滋病厅长」刘全喜，却一路官运亨通，02年当选中共十六大代表，「光荣」退休后，又继续担任河南省人大代表，主管教科文卫工作，没有受到丝毫惩罚。反而揭发血祸情况，为艾滋病人孤儿们奔走的高耀洁、胡佳、万延海等人，却遭受到了各种打压。<br />
<br />
爱滋宝宝及孤儿名单<br />
<br />
以下是部份爱滋宝宝及孤儿的个案，大部份资料取自高耀洁医生《血灾 一万封信》一书。对比起数以万计的爱滋宝宝及孤儿，以下只是冰山一角。<br />
<br />
（一）爱滋宝宝：<br />
<br />
1. 孙迎晨：河南商丘人，母亲李喜阁在1995年生产迎晨时，因剖腹产手术输血感染艾滋病，迎晨因母婴传播感染了艾滋病，2004年去世时只得9岁。妹妹林林亦母婴感染了艾滋病。<br />
<br />
2. 邸伉伉：河南商丘人，因急性坏死性肠炎输血染艾滋病，幼小生命不停受捱残，终于2003年去世，只得4岁。<br />
<br />
3. 井柏森：河南驻马店人，1994年出生，出生后不久因「脐炎」输血染艾滋病。2001年死亡，死时不足7岁。<br />
<br />
4. 刘丹丹：河南驻马店人，因意外从房顶摔下需输血增加营养而染艾滋病。2002年在痛苦中死去。<br />
<br />
5. 帅帅（乳名）：家住豫东南农村，1998年因烧伤输血，在2002年死亡。<br />
<br />
6. 程威威：死于艾滋病。<br />
<br />
7. 林林：河南商丘人，母亲李喜阁生其姐时，因剖腹产手术输血感染艾滋病，而令琳琳亦母婴感染了艾滋病。<br />
<br />
8. 陈倩倩：河南人，1999年出生。母亲在1995年生她哥时输血染艾滋病，母亲在2001年去世。倩倩因母婴感染有艾滋病。<br />
<br />
9. 亮亮（乳名）：于1996年在郑州某医院输过血，2003年夏发病。<br />
<br />
10. 潘满意：出生于1988年，她和父母在2001年被确诊有艾滋病。父于2002年病逝，母亲因受病毒摧残，2004年双目失明。<br />
<br />
11. 尚晴晴：2000年出生，2003年被遗弃在某福利院门口，浑身长脓包，容易得病、发烧、腹泻。2005年被证实有艾滋病。<br />
<br />
（二）爱滋孤儿<br />
<br />
1. 汪洋两子一女：汪洋是河南双庙集村人，因卖血染艾滋病，2001年死亡，遗下两子一女。<br />
<br />
2. 杨绍人两子一女：杨绍人住在河南申桥乡吕塘村，卖血染艾滋病，1996年去世时仅得26岁。杨死后不久妻子改嫁，三个儿女被迫流浪。<br />
<br />
3. 玉印两孩子：玉印在2001年死于艾滋病，其妻子早于1999年已因艾滋病去世。玉印死时，大孩子10岁，小孩子7岁。<br />
<br />
4. 王龙魁（化名）：父亲和母亲分别在2000年及2001年因艾滋病死亡。母亲死时，龙魁只得9岁，正读小二。<br />
<br />
5. 余传宝和弟弟：父亲2002年死于艾滋病，母亲亦是感染者，当时传宝15岁，弟弟8岁，才上三年级。<br />
<br />
6. 黑蛋、莉绢：父亲在2000年因卖血死于艾滋病，遗下八个月大的儿子黑蛋及12岁的女儿莉绢。<br />
<br />
7. 骆狮子女儿：父亲骆狮子和4岁的弟弟于2002年先后死于艾滋病，当时骆狮子的大女儿只得14岁。在家人患病时，她要辍学在家照顾弟弟。<br />
<br />
8. 张蕊（化名）：河南驻马店人，母亲在2002年死于艾滋病，父亲张保（化名）亦染病。<br />
<br />
9. 王欢欢：2002年12岁。父母因卖血染上艾滋病死亡。<br />
<br />
10. 阎宏桢（化名）：2003年12岁。父母因艾滋病死亡后，正读小五的他被迫辍学，以乞讨和拾荒为生。后被山东的好心人收养，2007年更以优异成绩考入山东曹县重点学校第一高中。<br />
