&nbsp; &nbsp; “找个我有空的时间，让你感觉开心快乐”―――想起宋山木的这句话，22岁女孩罗云(化名)如今感觉恶心―――5月4日，她向深圳警方报案称，5月3日晚，她被这位山木集团总裁带到罗湖区松泉公寓，遭受威胁并被拍下裸照，身处月经期仍被强奸。 <br />
<br />
&nbsp; &nbsp; 多名知情人士向南方都市报记者证实，宋山木已被列入网上追逃系统，警方则表示，正对此事进行调查。昨晚，山木集团的法律顾问杨先生联系本报称，事实并非强奸“情况复杂”。 <br />
<br />
&nbsp; &nbsp; 女大学生获聘山木 <br />
<br />
&nbsp; &nbsp; 1991年，宋山木在深圳创立山木教育集团(山木培训)，经过近20年的发展，成为国内最知名的培训机构之一。其蓄胡子微笑的形象广告牌，更是遍布深圳、上海、北京、济南等各大城市，让人印象深刻。 <br />
<br />
&nbsp; &nbsp; 今年春节后，网上出现一则“最牛春晚粉丝”的帖子，人们发现，从2005年起，每届春晚的观众席上都有宋山木热烈鼓掌的身影，其领结、吊带、白色上衣的装束屡次出现。尽管被疑为自我炒作，帖子仍然广泛传播，让宋山木在网上也名噪一时。 <br />
<br />
&nbsp; &nbsp; 入职山木集团前，罗云也早已见过这个大胡子的照片。2009年，山木集团来到罗云就读的安徽合肥一家职业技术学院招聘，当时就读英语专业的罗云即将于2010年7月份毕业，便欣然前往应聘并获选，于2009年秋来到济南培训。 <br />
<br />
&nbsp; &nbsp; 罗云一心想去上海发展，但其简历却被宋山木相中，分配到深圳工作，考虑到公司承诺一年后有调动机会，罗云安抚了父母的顾虑，于去年12月毅然南下。 <br />
<br />
&nbsp; &nbsp; 初入山木，罗云先后在广信酒店、深圳书城等教学点工作，教授《新概念英语》等课程，月薪1000余元；今年4月，她被调回位于国际科技大厦的总部，薪资在2000元左右。教学工作之余，罗云也被派到前台打杂―――打扫卫生、端茶送水中，曾与山木先生打过数次照面，却一直没有太多沟通，直到4月20日。 <br />
<br />
&nbsp; &nbsp; 申请辞职屡次遭拒 <br />
<br />
&nbsp; &nbsp; “当时东莞有家教育机构招人，我想要辞职过去，他可能看出来我情绪有问题，就问我怎么了，我不敢讲实话，就说是感情出了问题”，罗云说，宋山木花了半小时，与她讨论感情问题的解决办法，那是两人间唯一的长时间谈话。 <br />
<br />
&nbsp; &nbsp; 罗云不敢与这位总裁有过多接触，早先，她已从一位张姓同事处了解到一些传闻。张姓同事告诉罗云，宋山木与多位员工有超越工作的关系，并提醒她小心，但罗云对此总是半信半疑。 <br />
<br />
&nbsp; &nbsp; 综合考虑了薪资水平、职业前途后，5月3日下午，罗云与一名张姓同事走进宋山木的办公室，提出辞职申请。罗云提出的理由是，家人在上海开了外贸公司，要去帮忙做生意。宋山木努力挽留，称可以将她调去上海的山木培训机构，被拒绝后，他又要求罗云再坚持一周，以便能找到继任者。 <br />
<br />
&nbsp; &nbsp; 傍晚，宋山木将罗云叫入办公室。“他说，虽然提出了辞职，也不能表现出消极情绪，影响别人，辞职没办完前不能休息，要我跟他去一个地方打扫卫生，‘马上有人要搬过去住’”，罗云称，在总部自己做惯了杂务，并未觉得异样，只担心若是不从会影响到辞职，就随宋山木下楼，坐入他的奔驰S500汽车。 <br />
<br />
&nbsp; &nbsp; 受害者称被强拍裸照 <br />
<br />
&nbsp; &nbsp; 汽车一路东行，拐了数个弯后，罗云被带入一栋旧楼―――事后经警方调看电梯监控录像证实，5月3日晚8时01分，两人搭乘松泉公寓3栋电梯，进入809房―――罗云说，山木指示她打扫地板，清除一下房间里的霉味。 <br />
<br />
&nbsp; &nbsp; “我打扫了10分钟，他还在劝我不要离开公司，我不同意，他就发火了。”罗云回忆说，宋山木掏出手机，打给一个名叫“小三”的人，让对方照例“半小时内凭短信联系”，“打完电话他就恐吓我，‘这里是半山腰，掐死你都没人知道，如果你坚持要走，别人来掳走你，我就当什么都没看见，回头有人问我，我就说在路上换胎时你人不见了。’” <br />
<br />
&nbsp; &nbsp; 在这样的威胁下，罗云勉强同意留在公司，山木不信，将她从客厅带入里屋。“他要我脱了衣服，说这才表达诚意，我一迟疑，他又拿那个‘小三’威胁我，脱了工作服，他又让我脱内衣，‘来例假了也要脱’”，罗云对南都记者表示，自己坐在床上脱光后，宋山木掏出一台数码相机，让她说“我要留在山木工作”，并拍下约4张裸照，“他问了我两遍，说‘我要是把这些放到网上，会有什么样的后果’，我说，这一辈子我就完了，他就让我躺在床上。” <br />
<br />
&nbsp; &nbsp; 据罗云回忆，因正逢例假，宋山木给她垫上一条白浴巾、一条蓝浴巾，取来按摩棒刺激她，随后脱衣施暴，整个过程持续了约半小时，其中施暴过程约10分钟，未采取安全措施。 <br />
<br />
&nbsp; &nbsp; 罗云承认此前与男友相恋三年，已有性经验；由于正处经期，且不断受到威胁恐吓，未提出使用安全套。她记得，宋山木一边施暴，一边嗔怪，“我对你这么好，你怎么能这么伤我的心？” <br />
<br />
&nbsp; &nbsp; 事后，宋山木将罗云带到浴室洗澡，并要求她两次用牙膏清洗下体；由于其不断吹嘘已废掉了两个“情敌”，出于对男友的担心，罗云听从了命令。 <br />
<br />
&nbsp; &nbsp; 男友劝说下次日报警 <br />
<br />
&nbsp; &nbsp; 罗云表示，在返程中，宋山木不断许诺，可以将她调去上海并加薪，“我会经常去上海看你”，还提出要为她购置新衣、拍摄写真。 <br />
<br />
&nbsp; &nbsp; 当晚10时20分，罗云回到宿舍，彻夜无眠，在楼道哭泣，在男友的劝说下，她终于向附近的南园派出所报案，案件随后被转至罗湖区的东晓派出所。警方两次带着罗云寻找事发地点，最后确定，宋山木名下的松泉公寓旧宅正是事件发生的地方。罗云透露，破门而入后，警方找到了沾有血迹的蓝、白两条浴巾以及按摩棒，自己也将当天的衣物交给警方佐证。 <br />
<br />
&nbsp; &nbsp; 为取证，罗云被带到医院采集血样，并进行下体内窥镜检查。而山木方面已经知晓了报警一事。 <br />
<br />
&nbsp; &nbsp; 前天，为免人身危险，罗云决定离开深圳，按原计划到东莞入职；抵达后，她接到警方电话，获悉宋山木目前正在北京。 <br />
<br />
&nbsp; &nbsp; 知情者：宋山木名列公安追逃系统 <br />
<br />
&nbsp; &nbsp; 罗湖警方暂未确认，正全力调查 <br />
<br />
&nbsp; &nbsp; “总裁正在北京中央党校学习呢。”山木方面的杨律师昨日致电南都记者，“公安确实联系过他，要求他协助调查，山木先生已经回复说，过几天就回来接受调查。据我们了解，这个事的情况比较复杂，并不像那位小姐所说的是强奸，他俩原本关系挺不错的。”但昨日傍晚，南都记者致电山木培训全国热线，客服小姐却给出了和杨律师不同的说法，“总裁2天前去了南昌，进行新区域开发。” <br />
<br />
&nbsp; &nbsp; 昨天中午，为罗云代理法律事务的赵律师对南都记者透露，在全国联网的追逃系统上已经能查到宋山木的信息，这一消息随后也得到了多名知情人士的证实。 <br />
<br />
&nbsp; &nbsp; 但对此，罗湖警方未予确认，一位负责宣传工作的警官表示，不清楚宋山木是否已被通缉，目前警方正全力调查此事，相关细节尚无法确认。而杨律师也否认“通缉”一说，称至今未听说此事。 <br />
<br />
&nbsp; &nbsp; “从目前的证据来看，发生性行为基本可以确认，但是否属于强奸，要看其中是否存在强迫性。”昨天下午，赵律师告诉南都记者，“我们只能从两方面推断强迫性成立，一是受害者当时正在经期，不可能同意行房，二是两人原本并不熟悉，更不是情侣关系。” <br />
<br />
&nbsp; &nbsp; 实际上，类似传闻早在2008年就出现在网上。新浪网友“思竹之声”2008年3月在其博客上发文，将宋山木称为“中国最大的流氓企业家”，并质问其“强奸了多少好女孩”？博文中称，“凡是新招来的女孩，年轻漂亮一点的，他都想方设法在工作中通过各种机会，寻找各种借口把她们占为己有。” <br />
<br />
&nbsp; &nbsp; 就此，南都记者希望向杨律师了解更多详情，至昨日发稿时为止未果。 <br />
