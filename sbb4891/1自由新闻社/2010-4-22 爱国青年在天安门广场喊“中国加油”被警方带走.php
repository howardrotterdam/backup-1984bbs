全国下半旗9常委默哀一分钟<br />
<br />
昨日上午，中南海举行中共政治局常委会议前，国家主席胡锦涛，总理温家宝等9名常委肃立，集体默哀一分钟。在官方报道中，卸任国家主席的江泽民仅次排列在胡锦涛之后，位居悼念者名单的第二名。<br />
<br />
上午10时，青海省会西宁市数万民众亦冒雪在新宁广场集会悼念，集体默哀3分钟，同时汽车，火车，轮船鸣笛，防空警报响起。省长骆惠宁致辞说：“一个个鲜活的生命凋零陨落，三江流泪，昆仑呜咽... ...“同时，各地也举办不同形式悼念活动。在北京天安门广场，凌晨5时28分，天安门广场升旗仪式结束后，国旗降至旗杆一半，数千名民众围观，部分人还落下眼泪。<br />
<br />
相较2008年汶川大地震，今次悼念活动规模较小。昨日在天安门广场，当局并无举办悼念仪式。<strong>但10时许，数千民众聚集国旗周围，部分民众自发默哀3分钟。一名青年喊出：“中国加油！玉树加油！”但他随即被现场便衣警员带离现场。</strong>现场也未出现悼念汶川地震时，整个广场齐喊口号的现象。<br />
<br />
中央官网大红大绿挨批<br />
<br />
与内地各大网站首页昨日“素颜”不同，中央政府门户网站<a href="http://www.gov.cn" target="_blank">www.gov.cn</a>全天仍是大红大绿，遭到内地网民痛批。有内地网友致电询问，网站回覆称，他们未接到上面转色通知，国徽也不能改变颜色，网站已有黑色横幅。有网民直言，政府网上包含多幅现任领导人肖像，当局是避免“黑白相片”的忌讳。<br />
