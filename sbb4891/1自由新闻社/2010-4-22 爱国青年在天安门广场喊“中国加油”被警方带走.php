（明报）2010年4月22日星期四05:10<br />
<br />
【明报专讯】昨天是青海玉树地震遇难者的“头七”，全国各地降半旗志哀，所有娱乐活动，体育赛事等均停止，内地各家报纸头版及各网站首页亦改为黑白，但中央政府网站（<a href="http://www.gov.cn" target="_blank">www.gov.cn</a>）仍大红大绿，遭到内地网民痛批。<br />
