													<h2>人民时评：美国互联网外交是虚伪的实用主义</h2>
						
						
																											<div id="postmessage_447087" class="t_msgfont">人民时评：美国互联网外交是虚伪的实用主义<br />
2010-01-22 18:06:58　来源: 人民网(北京)　<br />
国际社会不能为美国的互联网外交所迷惑，必须通过有效的技术手段，抵御美国在虚拟空间发起的全面渗透和侵入。<br />
<br />
<br />
周四晚间，美国国务卿希拉里就网络自由发表讲话。批评中国的意味浓厚---指责中国对网络信息进行限制；要求中国对包括谷歌在内的美国公司所受网络攻击进行“彻底透明的调查”；声称“限制信息享用自由，或损害互联网用户基本权利的国家有跟不上下一个世纪发展进程的风险”；表示愿以“坦率且始终于一”的态度化解与中国在互联网自由方面的分歧。<br />
<br />
前有谷歌事件的发酵，后有希拉里的网络讲话。虽然其助手声称并非针对中国，但明眼人看得出，美国是利用谷歌事件拿中国开刀，并在全球范围内覆盖起一张网络外交的大网，作为推展“巧实力”的凭借，维持其在“后危机”时代的一超独霸地位。对此，美国媒体将之成为新的“克林顿主义”。其实，这个“主义”和美国曾经推行的其他“主义”（如新孤立主义、新干涉主义、新保守主义等）没有什么根本的不同，撤掉覆盖在美国互联网外交上自由和民主的矫饰面纱，祛除老生常谈的价值观内核，就只剩下了虚伪的“实用主义”。<br />
<br />
所谓“虚伪”，是指美国一直在互联管理上秉承双重标准，说一套做一套。<br />
<br />
从希拉里国务卿对中国网络管理的指责看，似乎美国国内的互联网管理绝对“自由”，网民享有无障碍的“冲浪”权力。其实不然，根据美国的爱国者法案，为了反恐需要警方有权搜索公民的电子邮件通讯，甚至可在不经允许的情况下监视公民通讯。据美国司法部的调查报告，美国联邦调查局(FBI)在2002-2006年间，通过电子邮件、便条和打电话等方式，窃取数千份美国民众的通话记录。此外，据2007年度美国《信息自由法》解密文件显示，美国联邦调查局(FBI)创建有“数字信息收集系统网络”，用于秘密窃听和监控邮件。<br />
<br />
此外，在美国经常指责中国黑客攻击美国网站，并要求中国调查谷歌被攻击的时候，殊不知美国建有专门的黑客部队，而且在全球范围内招募黑客精英为美国服务。美国每年都在赌城拉斯维加斯举行黑客大赛，甚至将这种比赛拓展到海外。据美国防务专家乔尔・哈丁估计，美军目前约有3000-5000名信息战专家。值得一提的是，在美国叫嚣谷歌被攻击的时候，中国第一大的搜索引擎百度也被攻击，而百度的域名注册服务商就是美国的“Register.com inc.”。美国在指责别国的同时，美国是不是也该调查一下本国黑客的作为？<br />
<br />
事实上，互联网不过是现实世界的延伸，虽说在虚拟空间没有国界之分。但由于现实世界的深度介入，因此主权观念和法治监管还是必要的。没有人怀疑美国对本国互联网的有效监管，但是如果说美国只承认本国对互联网监控的合理性，而批评其他国家的监管。显然就是“只许州官放火，不许百姓点灯”，不仅霸道，更是虚伪。<br />
<br />
所谓“实用主义”，是指美国为了本国利益，什么手段都可以采用，这个时候就忘了所谓的价值观，也不管对方是盟友还是敌人了。只要对自己有利，美国就会出手。现实世界如此，互联空间也是这样。<br />
<br />
在对待美国界定的“无赖”国家上，美国政府除了动刀动枪和经济制裁，就是进行“信息制裁”。如2009年5月，微软公司就按照美国政府的授意，切断了古巴、伊朗、叙利亚、苏丹和朝鲜五国的MSN即时通讯服务端口。而在伊朗大选前后，美国为了支持选举失利的改革派，利用推特（Twitter）帮助反对派制造舆论声势。可见，在对待敌国的问题上，互联网是一种实用的制裁工具，互联网充当着美国政府颠覆这些国家的“打手”和“帮凶”作用。<br />
<br />
即使是对待欧洲这样的盟国，美国也会利用信息技术支撑的全球电子监控系统，搜集一些商业机密，提供给本国企业。如帮助波音公司和欧洲空中客车争夺民航订单等。<br />
<br />
<br />
在对待中国的问题上，互联网成为美国发泄对华意识形态意见的主要载体，并成为遏制中国的新手段。美国除了批评中国的网络监管，还支持反华势力在美设立各类网站，诋毁中国形象，并提供和升级各类破解工具，误导中国网民。<br />
<br />
值得一提的是，在美国利用互联网推行所谓民主的价值观、干涉他国内政的时候，微软、谷歌等互联网巨头，立刻成为本国政府的棋子，政商联动，商业角色被政治所劫持。在此情势下，微软和谷歌在其他主权国家的商业布局，事实上就成为美国利益渗透的毛细血管。<br />
<br />
只要美国掌控着互联网的绝对霸权，美国政商两届就会利用互联网这一新型工具，实现国家利益的最大化。因此，国际社会不能为美国的互联网外交所迷惑，必须通过有效的技术手段，抵御美国在虚拟空间发起的全面渗透和侵入。<br />
<br />
(本文来源：人民网 作者：张敬伟)</div>

							
							
							
							
