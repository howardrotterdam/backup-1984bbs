													<h2>网络媒体协会会长:中国互联网上的交流是活跃的</h2>
						
						
																											<div id="postmessage_447095" class="t_msgfont">来源：千龙网 2010年01月22日<br />
　　中国互联网上的交流是活跃的<br />
<br />
　　北京网络媒体协会会长 闵大洪<br />
<br />
　　美国务卿希拉里1月21日发表演讲，影射中国没有信息自由，没有言论自由，这是不尊重事实的，也是站不住脚的。<br />
<br />
　　就在几天前，CNNIC发布了第25次中国互联网络发展状况统计报告，统计显示，截至去年底，中国网民规模达3.84亿人，网站数量达到323万个，互联网普及率超过世界平均水平。短短15年间互联网发展达到如此规模和水平，对一个发展中国家来说，是一个很了不起的成绩。这充分说明，中国的互联网发展环境是好的。试想，如果中国的互联网环境不好，怎么可能成长出新浪、搜狐、百度这样的知名互联网企业？如果中国的互联网环境不好，微软、雅虎、思科等境外互联网大公司又怎能从中国市场获得可观的利润呢？<br />
<br />
　　不可否认的一个事实是，中国互联网普及率大幅提高的同时，网上交流也十分活跃。就拿网络互动空间来说，中国现在有论坛上百万个、博客2亿多个，每6个中国人就拥有1个博客，网民每天新发布的博文超过400万篇，各类论坛每天新增的帖文更是难以计数。如此规模庞大的互动，说明中国互联网上的信息交流是开放的、充分的，也是自由的。<br />
<br />
　　中国从来不反对网络信息的畅通自由流动，但世界上没有绝对的自由，也没有绝对的所谓信息自由。信息自由的前提是必须遵守法律，必须在法律框架下有序流动。在中国境内的网上信息，必须受中国法律的管辖；在中国境内的外国企业在受到中国法律保护的同时，也必须遵守中国的法律法规，这些最起码的规矩，在中国如此，在美国也如此。对那些违反法律、损害公众利益、危害社会秩序的网上信息，应该而且必须依法进行管理，这已经是被公认的常识，世界上大多数国家也都是这么做的。难道美国能容许那些宣扬恐怖主义、推翻政府的信息在网上泛滥？政府能容许网上随意发布这些信息视而不见？将心比心，中国对于互联网的管理理应得到尊重和理解，而不是指责。<br />
<br />
　　再说，中国的互联网要怎么发展，怎么管理，那是中国人自己的事情，得中国人说了算。任何无端的指责都是别有用心的，也是绝不能接受的。中国有自己的国情，有自己的文化传统，有自己的做事原则，对于国家安全、社会稳定这些攸关国家民族利益的问题，中国绝不可能接受任何条件，不可能做出任何让步。说到底，在互联网的问题上，中国不需要美国教我们怎么做，做什么。</div>

							
							
							
							
