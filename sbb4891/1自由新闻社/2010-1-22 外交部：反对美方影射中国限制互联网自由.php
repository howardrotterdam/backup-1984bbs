													<h2>新华网：网络是自由的，也是法治的</h2>
						
						
																											<div id="postmessage_447398" class="t_msgfont">2010年01月23日 11:43:16 　来源：新京报<br />
<br />
有一组数字能说明中国互联网的现状―――到去年年底，中国网民达3.84亿，网站达368万个，博客1.8亿，中国的网民数每天在以20万人的速度增加，“中国是互联网发展最活跃的国家”。如果说中国限制了互联网的发展，那么，这些事实就是最好的反驳。<br />
<br />
仅以推动社会进步和反腐败为例，正是拜互联网的开放性所赐，像周正龙案、邓玉娇案、“躲猫猫”案等大量社会事件，通过互联网得以逐步呈现，并最终使真相大白，公平和正义得以伸张。而像“天价烟”周久耕案等政府官员的贪腐行为，也通过开放的互联网被网民呈现，并最终得以问责。事实上，在对待网络反腐问题上，中国政府的态度非常鲜明，网络反腐的运用越来越大。去年5月1日，由中共中央党校出版社出版的《党的建设辞典》第二版，正式收入了“网络反腐”这一词条，就是一个证明。<br />
<br />
众所周知，互联网的特性是开放和自由。但是正如世界上没有绝对的自由一样，互联网的自由也有自己的边界，那就是法律。每个国家都会制定相关法律，从本国法律出发依法管理社会，互联网作为信息传播技术和手段之一，自然也应被纳入法治的范畴，在各国法律允许的框架内运行。这本就是世界通行的做法，中国如此，美国也不会例外。<br />
<br />
在一个互联互通的世界，互联网对不同国家间人民的友好往来也会有极大的促进作用。但需要警惕的是，有一些人把互联网问题政治化、复杂化，正如过去以“人权”的名义对别国进行指责和干涉一样，现在又借“互联网自由”的概念，不考虑他国的实际情况，意图完全按自己的想法来影响和干预世界，以达到自己的政治目的，这并不现实，对其说“不”也是必然的。</div>

							
							
							
							
