人民网1月23日报道 美国国务卿希拉里发表演说，要利用互联网等高科技工具的力量，向全球推广美式信息自由，号称帮助全世界的人民访问自由的互联网，增强信息自由流动。然而，什么是所谓的信息自由流动。没有限制的就一定是最好，最自由的么？美国做到了么？ <br />
<br />
