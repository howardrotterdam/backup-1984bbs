<br />
中国法律禁止任何形式的网络黑客行为和侵犯公民个人隐私的行为。中国是世界上主要的黑客攻击受害国之一。中国主张通过加强国际合作，共同严厉打击网络黑客行为，维护网络安全和依法保护公民个人隐私。<br />
<br />
我们敦促美方尊重事实，停止利用所谓互联网自由问题对中国进行无理指责。我们希望美方同中方一道，认真落实两国领导人就发展新时期中美关系达成的共识，加强对话、交流与合作，尊重彼此核心利益和重大关切，妥善处理分歧和敏感问题，以维护中美关系健康稳定发展。（外交部网站）<br />
