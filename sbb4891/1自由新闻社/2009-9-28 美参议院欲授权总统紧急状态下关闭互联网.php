																											<div id="postmessage_694985" class="t_msgfont">中新网6月21日电 美国一项新法案将赋予总统奥巴马权力，于“国家紧急事态”下关闭因特网。据香港《文汇报》报道，法案一旦通过，奥巴马将有权命令Google和雅虎等受欢迎网络搜寻器暂时关闭，其他美国网络服务和宽频供应商也须遵从，违例将面临巨额罚款。<br />
<br />
　　法案由前总统参选人、参议院国土安全委员会主席利伯曼提出，但遭到美国最大科技游说组织TechAmerica批评，指法案让白宫“拥有潜在绝对权力”和“滥用权力”。<br />
<br />
　　然而，国家情报总监布莱尔年初曾指出，美国正受“恶意网络攻击严重威胁”，政府部门网络过去3年的受袭次数上升400%。<br />
<br />
　　根据法案，任何被国土安全部列为“依赖”因特网、电话系统或是任何美国“信息基建”的公司，都将受国安部内新设的国家网络保安及通讯中心指挥。 <br />
