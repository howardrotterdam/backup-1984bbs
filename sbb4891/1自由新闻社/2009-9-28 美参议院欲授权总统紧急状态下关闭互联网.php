																											<div id="postmessage_262441" class="t_msgfont">From: <a href="http://news.sina.com.cn/w/2009-09-28/173918745554.shtml" target="_blank">http://news.sina.com.cn/w/2009-09-28/173918745554.shtml</a><br />
<br />
<a href="http://www.sina.com.cn" target="_blank">http://www.sina.com.cn</a>&nbsp;&nbsp;2009年09月28日17:39&nbsp;&nbsp;国际在线<br />
<br />
　　国际在线专稿：据国外媒体报道，近日，美国参议院提出一个议案，计划赋予美国总统在紧急状态下关闭互联网服务的权利。可以看出，作为号称互联网最自由的国家，美国政府在互联网保护问题上该扮演什么样的角色已经成为美国社会各界热议的焦点。<br />
<br />
　　据悉，这个议案遭到了众多互联网企业和提倡保护隐私权者的反对。他们认为，美国政府不能这样控制互联网。虽然该法案目前被搁置，但是关于这个话题的讨论正激烈展开。在面临公共安全危机时，政府应该掌握多大程度上的网络控制权呢？又有多少控制权要留给私人机构呢？<br />
<br />
　　前任白宫电脑安全顾问梅里萨・哈萨维表示：“我们要为这场数字灾难做好准备。我们需要建立一个能迅速对网络攻击进行确认、判断的反应机制。”迄今为止，美国国会就“美国遭受大规模网络攻击的情况“赋予联邦政府保护国家的权力一事接连提出了至少18个议案，但是美国政府官员不希望“触碰个人与企业的隐私权”。<br />
<br />
　　在“9・11”恐怖袭击事件发生后的几个小时里，美国政府对境内的所有飞机做出了各种各样的严格禁令。那么在国家面对“数字灾难”的时候，美国总统能不能，该不该拥有这种相同的权利呢？如果电脑黑客控制了核电站的控制系统，美国总统是否该命令关闭该电站的电脑网络？如果遇到了恐怖袭击，美国政府是否该关闭电脑网络以保证关键系统的运作？美国政府是否有权决定强制保护与美国人日常生活密切相关的互联网的安全？<br />
<br />
　　随着近年来的互联网安全形势与国家安全形势的发展，网络安全问题吸引了美国各界的巨大兴趣，他们都希望通过立法授予总统更多的监督权来确保美国安全。<br />
<br />
　　据悉，美国众议院国土安全委员会的法案将对国家电网进行保护，并要求确保电网的网络安全。参议院的商业、科技和运输委员会正在酝酿一个法案，把白宫的网络安全规划提升至最高级别，并制定专业的网络标准。在此前的草案中，美国总统将被赋予在紧急状态下关闭受攻击联邦政府的或其他关键网络的权利。<br />
<br />
　　这一消息传出，众多美国网民惊呼政府正在接管网络。许多互联网企业领导人对该法案表示抗议。美国参议院表示，总统拥有宪法授予的权力，在面对危机时保护美国人民并“确保我们的国家的网络基础设施不被攻击”。据悉，美国官员承认，美国政府的官方互联网每天都被扫描或攻击上百万次。在今年7月，电脑黑客就“黑”掉了美国政府与韩国的几个网站。<br />
<br />
　　对于美国政府授权总统关闭互联网的法案，一些网络专家认为，当黑客渗透进一个关键网络时，解决方法除了必须关闭系统外，还必须隔离并滤出对方电脑的恶意代码。网络安全软件公司ArcSight的主席汤姆・赖利表示，政府应该巩固在关键基础设施和数据隐私方面的保护措施，并提高网络安全方面的教育，不是简单的一关了之。“人们总是想知道他们的电脑是否是全球那10%被感染的电脑之一，但他们就是不知道该做点什么。” （海澜）</div>

							
							
							
							
