这就是说，胡总书记在西柏坡学习考察两天时间，只花了30元钱的餐费。是的，我们的胡总书记到西柏坡学习考察，每天的餐费居然只有15块钱。 <br />
犹记得1月20日《南方都市报》曾报道过一组“安徽蚌埠国土资源局消费账单”，披露的据称是该局在当地一家酒店消费的四张“结账单”。从这四次消费金额上看，最低的一次是3900元，最高的一次则是11822元。其中，消费额最低的那次是四瓶600元一瓶的茅台酒和两条75元一盒的中华烟；至于消费最高的那次，则涉及到了三十多道饭菜、烟酒等，每道菜都是两例。 <br />
&nbsp; &nbsp; 这便是中国官场的一大“公害”――“公费吃喝”了。据报道，2008年，国家“三公消费””（公费吃喝、公费出国、公费用车）为9156亿元人民币（不包括公职人员的福利津贴、奖金等），比原预算288亿元多花了8868亿元。 <br />
&nbsp; &nbsp; 这9156亿元人民币“三公”消费里，有多少属于“公费吃喝”呢？我们不得而知，但我可以提供一个参数：《新民晚报》曾有一则《了望》周刊的文章摘要说，2004年全国公费吃喝超过了3700亿元。这就是说，2004年全国公费吃喝就已经是3700亿元人民币了！ <br />
&nbsp; &nbsp; 我们不妨来做个简单的算术题：我们把2008年的公费吃喝（假定也是3700亿元），除以全国的公务员人数（1100万），那么平均每个公务员的公费吃喝就是3.4万元，也就是说，每人每天的“公费吃喝”是100元，比我们总书记的每天15元饭餐费要高出6倍。 <br />
&nbsp; &nbsp; 反之，如果我们的公务员们也像我们的总书记一样，每天只花15块钱餐费，那么，我们的“公费吃喝”就会从3700亿元的天文数字，锐减为600亿元。虽然600亿元仍然不容小视，但却为国家节约了3100亿元开支。 <br />
&nbsp; &nbsp; 我们可别“小看”这3100亿元。2008年全国国家财政中教育经费开支是8280亿元，3100亿元可以让全国教育经费增加30%；2008年中国军费预算是4177亿元，3100亿元可以支付国家军费的74%；中国有2900万的贫困人口（标准是人均年收入688元），3100亿元可以让中国不再有贫困人口；三峡工程总投资是2000亿元，3100亿元可以每年建1.5个三峡工程。由此可见，公费吃喝对国家的危害到底有多大。 <br />
