													<h2>被出卖的台湾（Formosa Betrayed）</h2>
						
						
																											<div id="postmessage_496156" class="t_msgfont">被出卖的台湾（Formosa Betrayed）(2010-01-20 18:27:48)<br />
<br />
★影片介绍取自Formosa Betrayed<br />
 <br />
这部电影目前在台湾都还鲜为人知，我真诚的推荐各位如果有机会的话，看看这部电影。透过这部电影的视角，您可以了解七八0年代台湾政府在诡谲多变的国际氛围中的困境与挣扎，也可以带领您了解台湾的国民党政府的党国体系如何分崩瓦解，台湾的政体如何逐渐的走向民主化的过程。<br />
<br />
『「被出卖的台湾」(Formosa Betrayed) 是好莱坞有史以来第一部描述70及80年代台湾民主及独立运动人士奋斗过程的电影，在内地或许此一议题有其政治的敏感性，导致影片可能不会公开上映，或许更不会有相关信息，但我相信内地的人应该都有办法在PPS、PPlive或迅雷等影音平台搜寻得到。电影情节取材自遇害的陈文成博士、记者江南、及在白色恐布时代无数牺牲性命的民主及独立运动人士。』<br />
<br />
透过它，您将了解台湾的成长与过去。<br />
<br />
故事简述<br />
 <br />
在侦查一个小镇里台美人教授被谋杀的案件中，一位对司法公正己失去信心的警探，慢慢地发掘一些在美国大学校园里秘密进行多年的国际阴谋。借着这被害者的妻子所提供的线索以及一个台湾派来的间谍之助，这个警探发现他正走向一道不归途-正要撞上美国国务院、中国黑社会的帮派组织、更不可思议的，也终将撞上国民党政府的最高阶层。<br />
 <br />
背景<br />
 <br />
虽然有很多好莱呜的影片曾试图带领美国观众探索中国文化与政治，但是却未曾有过一部电影试图探寻台湾特殊的政治历史。美国影片如 ”Red Corner”, The Last Emperor”, “Crouching Tiger, Hidden Dragon” 是众所周知的有关中国社会文化的影片，但却不涉及台湾及美国所面对的政治社会议题。<br />
 <br />
“Formosa Betrayed” 是要藉由一位台美人教授为追求台湾的民主独立而被谋杀的人间悲剧，要让全世界的观众了解台湾追求自由民主的史实。<br />
 <br />
根据1970及80年代台美人教授及台湾来的留学生所遭受到的事实， “Formosa Betrayed” 要暴露在台湾的国民党政府为巩固独裁排斥异议所用的残忍手段。<br />
在台湾及中国之外的人，大都不了解台湾政治地位的历史背景。 ”Formosa Betrayed” 将要带领全世界的观众来了解台美关系背后蕴藏的人间故事。<br />
 <br />
我们相信 “Formosa Betrayed” 对美国观众、亚洲观众、及欧洲观众将会有深远的影响，因为它将透过一般所熟悉的大众博播工具，从特殊的文化观点来表达。再有，因为一般人对台湾政治历史缺少了解，像 “Formosa Betrayed” 的一部电影将能使观众了解台湾人民善良的特质。<br />
 <br />
故事<br />
 <br />
“Formosa Betrayed”是一部有关台美人异议人士在1980年代受恐吓、受迫害、被谋杀的戏情片。根源于真实故事，这部影片焦聚在堪萨斯大学一位台美人教授的被谋杀事件。一个对台湾毫无了解的警探被派调查这个案子。透过对这个案子的侦查，这个警探逐渐地理出存在于台、美、中政治、意识、与权势错综复杂的关系。<br />
 <br />
在他侦查谋杀者及其共犯的过程中，这个警探发现在美国大学校园里存在着一个大间谍网，专门监视台湾人及台湾留学生的政治与社交活动。他求助于美国联邦调查局，随即发现联邦调查局己知道这个间谍网的存在；但是联邦调查局不愿采取行动，只为了保护美国在台湾及其他地区的间谍网。<br />
 <br />
这个警探的侦查工作，引领他从中西部，遍查到其他地方的大学校园、中国城、最后追查到台湾。就在台湾，他查出这台美人教授的被杀害是因为他大力倡导台湾独立，动摇蒋介石政权的正当性。最高当局下令追杀。<br />
 <br />
这个警探带着查出的证据，走访美国在台协会；竟然被告知这涉及美国外交政策的敏感问题，不能受理。明知不义，又无能为力，失望之余，他回到美国。终于，他被邀到国会作证，公开了这谋杀与校园间谍的真相。<br />
<br />
 <br />
<br />
<br />
