 <br />
以下文章转载自「台湾青年」及「Formosa Betrayed」。<br />
 <br />
[好莱坞首部台湾民族大戏] 被出卖的台湾 Formosa Betrayed / Will Tiao 2008大选前上映热烈期待<br />
 <br />
「被出卖的台湾」(Formosa Betrayed) 是好莱坞有史以来第一部描述70及80年代台湾民主及独立运动人士奋斗过程的电影。电影情节取材自遇害的陈文成博士、记者江南、及在白色恐布时代无数牺牲性命的民主及独立运动人士。<br />
 <br />
故事由Will Tiao及好莱坞编剧依白色恐布时期真实事件改编而成。电影场景由一名美国干员调查1982年陈文成教授谋杀案开始。这名干员发现陈教授曾被国民党派遣的特务学生所监控，于是他远赴台湾缉凶，才发现原来这椿命案其实是国民党政治阴谋的一部份，谋杀陈文成教授目的是为了镇压刚萌芽的台湾独立运动。<br />
 <br />
Will Tiao对政治并不陌生，在进入好莱坞前，他有十年政治相关工作经验。他毕业于 Tufts 大学，主修国际关系。之后在哥伦比亚大学拿到国际事务学硕士。他是傅尔布莱特奖学金公费生，并在克林顿时代担任总统管理研究员，后来在布什总统时代成为国际经济学家。他曾多年参与促进台美关系工作；他的第一个工作是在台湾人公共事务会(FAPA)当实习生，当时他为台湾加入联合国进行游说工作。之后，他为参议员Nancy Kassebaum (Kansas) 效命，从事参议院外交关系委员会相关工作。他曾任职于国际贸易委员会下的一个次委员会；国际贸易委员会负责同意给予中国最惠国待遇。他也曾亲手拟订给予台湾最惠国待遇的条文。<br />
 <br />
2002年，Will Tiao离开政府公职转战演艺事业，希望发挥演艺专长来支持台湾。经过多年在独立制片电影的演出，终于如愿成为制作人，并在得?作品&quot;A Starbucks Story&quot;担纲演出。该片赢得2005年FAIF洛杉矶电影节最佳短片及最佳电影奖。他的演艺事业渐入佳境，在各大电视台频道时常能看到他身影。他最近又制作一部黑色喜剧&quot;Stan&quot;，己进入杀青阶段。在他所有的作品中，他认为「被出卖的台湾」是到目前为止最重要作品，他希望经由好莱坞系统，让这部片成为其它台湾题材电影问世的敲门砖。为了能让「被出卖的台湾」完成拍摄，Will Tiao成立了福尔摩莎电影公司，专门开发以台湾的故事为题材的电影。<br />
 <br />
二年前，Will Tiao巧遇福尔摩莎基金会创办人吴澧培先生。吴先生告诉他，拍摄一部以台湾为主题的电影是他人生的一个梦想。之后，Will Tiao努力完成了「被出卖的台湾」的剧情大纲，并且聘请好莱坞编剧加以润饰。他成立福尔摩莎电影公司后开始鼓励台湾美国人投资这部片，并以此争取认同。<br />
 <br />
2005 年12月，FAPA在德州达拉斯举行年会，在此Will Tiao进行他的首次募款，他啼声初试，共募得了20% 的前制资金。此后，他在美加20个城市共进行了23场募款活动，为此，北美台湾人均对他的努力印象十分深刻。Will Tiao将于东岸台美人大会(TAC-EC)以及加拿大多伦多台湾人议会中发表演说，目的是希望能于2006年完成募款、2007开拍，于2008年发行，并在三月台湾总统大选及八月北京奥运会前上映。<br />
 <br />
Will Tiao已聘请好莱坞资深制作人David Allen Cluck制作「被出卖的台湾」，Cluck有超过30部电影及电视剧制作经验，其中包括奥斯卡得奖影片&quot;Monster&quot;（由Charlize Theron主演)。Will Tiao另外聘请Dominique Forma担任导演，他最近的作品是由Sony Picture发行、Jeff Bridges 及 Noah Wyle 主演的 &quot;Scenes of the Crime&quot;。<br />
 <br />
Tiao表示：「多年来，台湾人总希望看到一部能呈现台湾人追求国家认同百折不挠、艰苦奋斗的电影，类似为犹太人拍的辛德勒的名单」，「这部片主要呈现两个基调－－正义与认同，这是台湾人小区中已完全理解与接受的两种价值观。」Tiao说：「这部片对台湾人是一考验，不像南美或者前苏联，台湾过去从未有因应独裁政权的经验。此时是团结台湾人力量完成这部片的时刻，如此才能让全世界了解作为一个台湾人真正的意义。」<br />
 <br />
