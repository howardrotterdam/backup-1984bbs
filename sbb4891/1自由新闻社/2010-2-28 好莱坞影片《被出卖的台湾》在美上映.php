																											<div id="postmessage_496142" class="t_msgfont">好莱坞影片《被出卖的台湾》即将在美上映<br />
<br />
2010-02-22<br />
<br />
一部描述台湾当年国民党统治的白色恐怖时期，海外政治异议人士被谋杀的故事片，将从26日起在美国上映。这是一部好莱坞影片，影片的名字叫做《被出卖的台湾》（Formosa Betrayed）。自由亚洲电台特约记者CK报道<br />
<br />
《被出卖的台湾》这部影片，以上个世纪70、80年代，国民党统治的白色恐怖时期的台湾为背景，讲述一位美国大学的台湾裔教授，因从事争取台湾民主自由和台湾独立运动，被台湾特务谋杀的故事。故事通过一位美国警探介入案件调查而展开。<br />
<br />
影片制片人是美籍台湾人刁毓能，他在旧金山推广这部影片的时候说：“这是特别针对美国观众，使他们更多了解台湾、美国、中国之间关系的影片，是第一部以此为题材的好莱坞影片。”刁毓能说：“很多美国人都越来越关注台湾究竟发生了什么，特别是年轻人。我在很多美国大学演讲，大学生们都对台湾发生的事情有浓厚兴趣。”<br />
<br />
影片导演亚当&#8226;凯恩说：“作为一个美国人，我们很少知道，有很多其他的国家的人，在为自由民主而挣扎。自由民主是我们国家的价值观，我们从小就认为这是天经地义的事。”他又说：“我读了很多关于台湾人以及有关台湾20世纪以来的文献，使我作为导演，能够更好把握这部影片。”<br />
<br />
《被出卖的台湾》制作成本一千万美元，影片四分之一制作费用来自旧金山台湾人的投资。北美台湾商会联合总会会长林贵香是投资者之一，她说：她并不是认为这部影片能赚钱才投资，而是为影片的故事所感动，“完全是台湾人的故事，借这个机会来介绍台湾让全世界都知道，所以我就第一个投资了。”<br />
<br />
台湾228纪念日又将到来，《被出卖的台湾》选在2月26日，在美国纽约、旧金山、洛杉矶、波士顿等城市同时上映。<br />
<br />
以上是自由亚洲电台特约记者CK的报道。<br />
