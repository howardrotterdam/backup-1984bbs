																											<div id="postmessage_531574" class="t_msgfont"><img src="http://i892.photobucket.com/albums/ac121/wodetuchuang/00-1.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
中国 更新时间 2010年 3月 24日 星期三 05:10 PM 格林威治标准时间<br />
<br />
<strong>GODADDY宣布不再提供新的.CN中文域名</strong><br />
<br />
记者: 张蓉湘 | 国会<br />
<br />
<br />
　　全球最大域名注册商GODADDY 星期三宣布，该公司不再提供新的.CN中文域名。据信该公司是继谷歌之后，第一家部分撤离中国相关业务的美国公司。在这之前，中方加强对因特网地址的域名注册管理。<br />
<br />
　　<strong>*担忧新政策影响用户安全*</strong><br />
<br />
　　根据美国之音事先获得的国会书面证词，一位GODADDY的高级主管24号告诉美国国会及行政部门中国问题委员会，中国国家互联网络信息中心对于域名注册的管理越来越严格。由于担忧中方对域名注册的新政策可能影响用户安全，以及它可能带来的寒蝉效应，该公司决定不再提供新的.CN域名，但会继续管理已经拥有.CN域名的现有用户。<br />
<br />
　　GODADDY总部位于美国亚利桑那州，是全球最大域名注册商，根据多家监测机构显示，目前放置在GODADDY上的网站数量已经越居第一位。该公司从2005年4月开始提供.CN的顶级域名。<br />
<br />
　　<strong>*中方加强域名注册管理*</strong><br />
<br />
　　一位GODADDY的高级主管指出，注册并且拥有.CN域名的中文网页，对任何希望进入中国市场的企业来说是个重要步骤。刚开始中国国家互联网络信息中心只要求GODADDY提供用户的名字、地址、电话与电子邮件帐号。不过，从去年12月14号开始，中方实施新的政策，要求域名注册者提供彩色照片、企业注册证明、以及注册者的签名表格，中方会进行事先审查。这项新的政策让许多通过GODADDY注册中文网页域名的用户受到影响，也是该公司第一次被要求追溯既往，要求已经注册过的用户提供额外资讯与文件。中方并且一度要求中国民众不得通过非中国域名注册商注册域名。<br />
<br />
　　美国之音尚未获得中国政府对此的评论。</div>

							
							
							
							
