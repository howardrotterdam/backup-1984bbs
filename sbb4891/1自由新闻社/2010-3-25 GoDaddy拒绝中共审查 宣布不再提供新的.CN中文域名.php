													<h2>GoDaddy to China: No More Domains for You</h2>
						
						
																											<div id="postmessage_531705" class="t_msgfont">JR Raphael, PC World<br />
Mar 25, 2010 6:41 am<br />
GoDaddy is joining Google in its fight against China's Internet control.<br />
<br />
The company has announced it'll no longer register new domains in the People's Republic -- and it has some strong reasons why.<br />
<br />
GoDaddy divulged its decision during a congressional hearing on Wednesday. Christine Jones, the company's executive VP, delivered a prepared statement explaining the shift in strategy.<br />
<br />
The nine-page testimony details an apparent change in Chinese law that requires GoDaddy to now collect color headshot photos of all users trying to register new domains. In addition, the company must obtain business identification documents and physically signed registration forms from all of its Chinese customers.<br />
<br />
Here's the kicker, though: All that data has to be forwarded on to China's government-run Internet Network Information Center (CNNIC) for &quot;review.&quot;<br />
<br />
Sound fishy to you? It did to GoDaddy, too.<br />
<br />
&quot;The intent of the new procedures appeared, to us, to be based on a desire by the Chinese authorities to exercise increased control over the subject matter of domain name registrations by Chinese nationals,&quot; Jones' testimony states.<br />
<br />
And that's not all, either: The testimony goes on to reveal that GoDaddy has been told to apply the new rule retroactively, obtaining photo identification from every existing China customer and supplying it to the government. If someone's data isn't turned over, Jones says, his domains are turned off.<br />
<br />
&quot;We are concerned for the security of the individuals affected by CNNIC's new requirements, as well as for the chilling effect we believe the requirements will have on new .CN domain name registrations,&quot; the testimony explains.<br />
<br />
So far, GoDaddy's the first major Internet company to follow Google's lead in the China Internet challenge. Microsoft's explicitly said it has no plans to make any changes. And, let's be honest, Apple practically uses the same oppressive tactics as China's regime, so it's not likely to be taking a stance anytime soon.<br />
<br />
All joking aside, though, don't think the division's going unnoticed. According to CNN, U.S. lawmakers at Wednesday's hearing praised Google and GoDaddy's efforts and slammed others for not following suit.<br />
<br />
&quot;They need to get on the right side of human rights rather than enabling tyranny, which they're doing right now,&quot; Rep. Chris Smith is quoted as saying.<br />
<br />
Of course, this is only the beginning; we can expect to hear more fighting words from all sides as the global battle rolls on.<br />
<br />
But hey, on the upside, at least this whole debacle will make for a brilliant movie some day. Just imagine the possibilities...<br />
