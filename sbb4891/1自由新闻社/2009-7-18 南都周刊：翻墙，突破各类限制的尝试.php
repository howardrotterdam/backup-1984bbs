													<h2>南都周刊：翻墙，突破各类限制的尝试</h2>
						
						
																											<div id="postmessage_167815" class="t_msgfont">设了一百道墙，网民们也不会像文章开头说的那两位，会重新翻回去。看看目前翻墙软件的热闹，应该能了解，许多人正在翻得不亦乐乎呢。<br />
<br />
文・目林寺<br />
<br />
<img src="http://nbweekly.oeeee.com/UserFiles/fckfiles/2009/07/03/3dd06271f73f4bd688007d33f6b90486.gif" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
词汇释义：<br />
 <br />
　　原指爬过越过围墙之类的肢体行为。现指通过各种代理服务器，突破限制，浏览或登录某些网站。网站整顿开始，尤其绿坝出来以后，该名词被网民不断使用，而被应用的软件则称为“翻墙软件”。其实，广义来说，所有试图突破限制的行为都可以称作“翻墙”。<br />
<br />
　　例句：昨晚翻墙去网吧，到了网吧，我又翻墙才看到自己喜欢的网站。<br />
 <br />
　　医院为防止病人出逃外设100道，两精神病患者仍欲逃出医院。<br />
<br />
　　于是，等到夜黑时刻，两人开始努力翻墙。至第30道墙下，“累了么？”“不累。”于是二人继续向外翻。至第60道墙下，“你累了么？”“不累。”于是二人继续向外翻，至第90道墙下，“你累了么？ ”“累了。”“那好，我们翻回去吧。” 于是，他们又吭哧吭哧往回翻。<br />
<br />
　　虽然是精神病患者，虽然最后没能成功，但这二位却体现着和所有人相通的点，那就是希望逃脱禁锢，向往墙外的自由自在。<br />
<br />
　　墙很多，无论是为了保障自身安全的墙，还是用于禁闭他人的墙，其基本功能是一样的，既是用于隔离。将一个空间和另一个空间隔离，使得人们无法在两处自由地来回。<br />
<br />
　　但人类追求自在的本性是与生俱来的。所以，自从有了墙这种形态，也就有了翻墙这种行为。翻越城墙进攻敌人，翻越院墙偷取财物，翻墙脱身，翻墙私会等等。也就是说，只要有人筑墙，必然有人翻墙，只要有人设置限制，就必然有人会去尝试突破。其实，广义来说，所有试图突破各种限制的行为都是一种翻墙行为。<br />
<br />
　　以前在学校的时候，我们经常“翻墙”去外面的小酒馆畅谈或是痛哭，以前校内的网络无法访问国外网站，我们就利用各种代理服务器“翻墙”达到自己的目的。前一个墙，翻越的是具体的用石头水泥砌的固体，后面那个要翻的则是网络上所设置的访问限制。<br />
<br />
　　自从去年网络开始整顿以来，许多网站因为自身内容的缘故，无法正常登录。关于翻墙的言论就开始在网络世界里满天飞，求翻墙软件的帖子到处都是，而在朋友之间也会开始互相推荐各类翻墙软件。这些使用翻墙软件的人们，正是试图使用代理访问的方式去浏览自己想看而又被屏蔽的网站。<br />
<br />
　　这些网站可能低俗，可能有不良内容。无论如何，屏蔽并没能完全阻止部分固执的网民。设定的限制只会促使他们用类似宵小的方式去达到自己的目的。<br />
<br />
　　最近，绿坝又被提上日程，据说会限制更多的不良网站浏览。等于又是给网民设置了多道墙，或许初衷是好的，或许保护了部分没有分辨能力的人。但是，有的墙真的就是用来翻越的，限制越多，人们翻越的欲望就越强。<br />
<br />
　　设了一百道墙，网民们也不会像文章开头说的那两位，会重新翻回去。看看目前翻墙软件的热闹，应该能了解，许多人正在翻得不亦乐乎呢。<br />
