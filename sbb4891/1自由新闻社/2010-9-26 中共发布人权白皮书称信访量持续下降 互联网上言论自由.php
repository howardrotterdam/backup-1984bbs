白皮书说，中国公民依法享有批评、建议、申诉、检举和控告的权利。中国政府通过开展绿色邮政、专线电话、网上信访、信访代理等多种渠道，为人民群众反映问题、表达诉求、提出意见建议提供便利。坚持实行党政领导干部阅批群众来信、定期接待群众来访、领导包案和责任追究等制度，切实维护人民群众的合法权益。<br />
<br />
2009年，中共中央办公厅、国务院办公厅转发《关于领导干部定期接待群众来访的意见》《关于中央和国家机关定期组织干部下访的意见》和《关于把矛盾纠纷排查化解工作制度化的意见》三个文件，将定期接待群众来访的主体从县委书记层拓宽到各级各部门领导干部，将组织中央和国家机关干部下访规范化，把矛盾纠纷排查化解工作制度化，进一步完善信访工作法规制度体系。<br />
<br />
中国公民网络言论自由受法律保护<br />
<br />
白皮书指出，中国公民在互联网上的言论自由受法律保护，可以通过各种形式在网上发表言论。据抽样统计，每天人们通过论坛、新闻评论、博客等渠道发表的言论达300多万条，超过66%的中国网民经常在网上发表言论，就各种话题进行讨论，充分表达思想观点和利益诉求。通过互联网了解民情、汇聚民智，成为中国政府执政为民、改进工作的新渠道。</div>

							
							
							
							
