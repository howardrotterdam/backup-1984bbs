<br />
白皮书介绍说，互联网得到广泛普及和运用，已成为人们获取各类信息和发表言论的重要途径。截至2009年底，中国网民人数达到3.84亿，互联网普及率达到28.9%，超过世界平均水平。中国境内网站达323万个。网站十分注重为网民提供发表言论的服务，约80%的网站提供电子公告服务。中国现有上百万个论坛，2.2亿个博客用户。<br />
<br />
白皮书指出，据抽样统计，每天人们通过论坛、新闻评论、博客等渠道发表的言论达300多万条，超过66%的中国网民经常在网上发表言论，就各种话题进行讨论，充分表达思想观点和利益诉求。通过互联网了解民情、汇聚民智，成为中国政府执政为民、改进工作的新渠道。中国领导人经常上网了解公众意愿，有时直接在网上与网民交流，讨论国家大事，回答网民的问题。各级政府出台重大政策前，通过互联网征求意见已成为普遍做法。近三年来，每年通过互联网征求到的建议多达几百万条，为完善政府工作提供了有益参考。<br />
<br />
白皮书还提到，中国政府十分重视互联网的监督作用，对人们通过互联网反映的问题，要求各级政府及时调查解决，并向公众反馈处理结果。绝大多数政府网站都公布了电子邮箱、电话号码，以便于公众反映政府工作中存在的问题，一大批通过互联网反映出来的问题得到了解决。为便于公众举报贪污腐败等问题，中央纪检监察机构和最高人民法院、最高人民检察院等开设了举报网站。(完)</div>

							
							
							
							
