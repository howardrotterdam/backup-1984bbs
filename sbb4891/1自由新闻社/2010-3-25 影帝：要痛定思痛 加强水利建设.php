													<h2>影帝：要痛定思痛 加强水利建设</h2>
						
						
																											<div id="postmessage_533115" class="t_msgfont"><img src="http://imgur.com/FK7Jr.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
3月19日至21日，中共中央政治局常委、国务院总理温家宝来到云南省曲靖市，深入旱灾最严重的地区，看望慰问受灾群众，指导抗旱救灾工作。这是3月20日，温家宝来到陆良县德格海子水库干涸的库底察看灾情。新华社记者 姚大伟 摄<br />
<br />
新华网北京3月25日电<br />
<br />
温家宝:坚决打好抗旱救灾这场硬仗――在云南考察旱情和抗旱工作时的讲话<br />
<br />
（2010年3月20日）<br />
<br />
目前，全国的旱情十分严重。截至3月18日统计，全国耕地受旱面积达9736万亩，有2054万人、1260万头大牲畜因旱饮水困难。旱灾最重的是云南、广西、贵州、四川、重庆五个省区市，受旱耕地面积8368万亩，占到全国受旱面积的86％。特别是云南、广西和贵州的干旱已持续了几个月，一些地方灾情达到百年一遇，对地区经济社会发展特别是农业生产造成极大威胁，给人民生活特别是城乡居民饮水安全带来很大困难。对这次旱灾，党中央、国务院高度重视，国家有关部门已经多次下拨抗旱救灾资金，派出工作组指导抗旱。面对灾情，各地抗旱救灾工作部署早、力度大，及时启动应急响应，紧急调拨资金、物资、人员，组织干部群众积极抗旱，付出了巨大努力，也取得了初步成效。我讲四点意见。<br />
<br />
一、切实把抗旱工作摆到当前工作的重要位置<br />
<br />
目前，整个西南地区的旱情还在持续，据气象预报，近期旱区不会有明显的降雨过程，干旱将持续蔓延，损失会继续增加，抗旱救灾的难度也会进一步加大。旱灾带来的危害，主要表现在以下几个方面:一是人畜饮水困难，越来越多的群众的基本生活受到影响。二是农业减产，不仅冬小麦、油菜等小春作物或者绝收、或者大幅度减产，而且春播、春种也难以进行，水稻、玉米等大春作物备耕和生产受到影响。三是推高物价，我注意到，云南、贵州等地已经出现因干旱引起的粮油等农副产品价格上涨。另外，干旱造成水电发电量减少，一些地方用电偏紧。可以说，旱灾既影响生活，也影响生产，影响整个社会的正常秩序。<br />
<br />
做好当前的抗旱救灾工作，事关人民群众的基本生活和经济社会发展的大局。今年，我国经济社会发展面临的形势十分复杂，要保持经济平稳较快发展和物价总体水平的基本稳定，必须千方百计保证今年农业有一个好收成。今年农业的状况，在一定程度上对国家发展的全局具有决定性的作用。我们要从全局的高度认识今年“三农”工作和当前抗旱救灾工作的重要性，以对人民高度负责的精神，不麻痹，不懈怠，进一步动员和行动起来，坚决打好这场抗旱救灾的硬仗，努力把灾害的损失降到最低程度。<br />
<br />
二、全面落实当前抗旱救灾和春季农业生产的各项措施<br />
<br />
当前抗旱救灾和春季农业生产，最重要的是解决好四个方面的问题。<br />
<br />
一是优先解决群众饮水问题。这是当前最为迫切的任务，要摆在抗旱救灾工作的第一位。要想尽一切办法，保证群众的生活用水。对旱区的缺水问题，要进行全面排查摸底，并根据实际情况制定饮水解困方案，落实供水措施。重点抓好现有水源的统一管理和调配，千方百计增加抗旱水源，加快应急水源工程建设，全面启动各类抗旱设施，尽可能多引、多拦、多蓄水。严重缺水的地方，要组织力量打井找水。确实无法解决水源问题的地方，要组织运水、送水，不惜代价，尽快缓解人畜饮水困难，绝不能让一个群众没有水喝。<br />
<br />
二是扎实做好春耕备耕。今年西南地区干旱，小春作物减产已成定局。去年冬天，北方地区入冬比常年提前20天，华北、黄淮北部等主产区的冬小麦提前 15－25天停止生长；今年开春，北方又持续低温，冬小麦的返青比常年晚10天左右。目前北方夏粮主产区尽管土壤墒情较好，但越冬作物苗小、苗弱的问题比较突出。在江淮和长江中下游地区，由于降水偏多，持续低温寡照，一些地方还出现严重渍害。所以，从全国来看，农业生产形势很不乐观。大家知道，我们已连续 6年农业增产，在50％的耕地还是靠天吃饭的情况下，确实来之不易。农业增产政策起了很重要的作用，但老天爷也帮了大忙。按正常年景，我们是“两丰两歉一平”，在连续6年增产后，今年能不能保住1万亿斤的总产量，这确实是个考验，首先夏粮丰收就面临严峻挑战。各地区、各有关部门要迅速行动起来，采取有效措施，从春耕备耕做起，扎实落实各项农业生产措施，努力克服灾害和不利天气的影响。要加大春季田间管理的力度，落实越冬作物促弱转壮的技术措施，促进苗情转化升级，加强病虫害的防治，做好化肥、农药、种子等供应，宣传和落实各项惠农政策，鼓励农民努力扩大春播面积，努力夺取今年农业丰收。<br />
<br />
西南受旱严重的地区，要通过调整结构、推广抗旱技术，化解农业生产难题。要根据不同作物的受灾程度，科学规划农业生产。受灾较轻的小春作物，要采取措施，尽量减少损失；受灾特别严重的，要及时改种。要依靠科技力量改变种植结构和模式，引导群众扩大旱育秧面积和旱粮播种面积，搞好水稻、玉米等大春作物的备耕。力争做到小春损失大春补、粮食损失经济作物补、种植业损失养殖业补、农业损失非农补，努力实现农业稳定发展、农民持续增收。 <br />
<br />
这是3月19日，温家宝来到陆良县芳华镇狮子口村万亩大麦示范田，实地察看农作物受灾情况。 新华社记者 姚大伟 摄<br />
<br />
三是严密防范森林火灾。持续的干旱使旱区的森林火险长时间处在高危险等级。一定要认真做好森林防火工作，严格落实责任制，加强宣传，加强火灾隐患排查，严密监测火情，完善应对预案，努力避免发生重特大森林火灾和重大人员死亡。<br />
<br />
四是保持灾区市场和社会稳定。要保证灾区粮油、肉类、蔬菜等市场供应，搞好重要商品物资的运输调度，加强市场监测和调控，防止价格大幅波动。要避免出现春夏粮荒和因旱致贫。西南旱区贫困面都比较大，有些地区一次旱灾就可以致贫。要制定切实可行的灾民生活救助方案，加大对受灾群众特别是重点旱区困难群体的救助力度，保障灾区群众的基本生活，维护灾区正常的生产生活秩序。<br />
<br />
三、加强对抗旱救灾工作的支持<br />
<br />
做好抗旱救灾各项工作，要切实加强领导，干部要深入一线，真正负起责任，落实好抗旱救灾责任制。各地区和各有关部门要密切配合，形成合力，进一步完善应急预案，把各项抗旱救灾措施落到实处。<br />
<br />
发展改革委、财政等部门要继续加大对旱区抗旱救灾资金物资的支持力度，加快应急水源等项目建设。抗旱救灾资金可打捆使用，中央部门不要把资金分得那么细，最好由省里去调配。水利、农业、林业等部门要组织力量，深入一线，对灾区抗旱、春耕、森林防火等有针对性地开展技术指导和服务，做好种子、种苗、抗旱设备的保障工作。气象部门要加强天气会商和灾情监测，适时开展人工增雨。粮食、商务部门要根据灾区市场变化，加强粮油等的调剂、调运，及时做好储备粮油、商品粮周转库存的出库，保证灾区粮油和各种生活品的供应和价格稳定。工商部门要加强市场秩序管理，打击各种扰乱市场的行为。电力、石油等部门要优先保证抗旱用电、用油。铁路、交通等部门要优先保证粮油等基本生活用品以及各种救灾物资的运输。民政部门要进一步加大对受灾群众的救助力度，妥善安排好灾区困难群众的生活。<br />
<br />
各受灾地区也要自力更生，继续增加抗旱投入，细化各项抗旱救灾的方案和措施，确保责任落实到人，措施落实到位，切实帮助群众解决生产生活中的困难。要一手抓抗旱救灾，一手抓经济社会发展，努力实现今年经济社会发展的各项目标。从根本上讲，搞好抗旱救灾工作，还得靠经济实力、靠经济社会的发展。现在，我们的条件比过去好得多，无论是从全国还是从受灾地区看，都有能力抵御住这场重大的自然灾害。因此，在工作中要特别注意统筹兼顾，抓好今年经济发展的各项任务。<br />
<br />
四、从长计议，大力加强水利设施建设<br />
<br />
我国农业已经是连续6年增产，6年间粮食产量增加2000多亿斤。但必须看到，我国农业基础薄弱的问题并没有根本改善，农业抗灾能力不强的矛盾仍然十分突出。这次受灾地区河流很多，常年雨量也不少，水资源总量排在全国前列，但时空分布极为不均，旱灾仍然列自然灾害之首。抗旱基础设施建设滞后，水源工程不足，灌溉设施不配套，山区人畜饮水困难，是地区经济社会发展的突出问题。要痛定思痛，下更大的决心，采取更有力的措施，加强水利建设。大中小水库统筹考虑，水库、塘坝与水窖统筹考虑。做到有水存得住，没雨时用得上。只有这样，才能从根本上增强抗御自然灾害的能力。<br />
<br />
水利建设要重点抓好三个方面:<br />
<br />
一是加强控制性骨干工程建设。西南旱区多是石灰岩地区，都存在水多、水少和水脏的问题。干旱的一个原因是，骨干蓄水工程少，工程性缺水问题突出。因此，拦水、蓄水、兴修水库就显得特别重要，就需要建设一些控制性骨干水利工程。这有利于提高供水能力，提高抗灾能力和水资源的利用效率，对解决城市及其他人口稠密地区的用水问题，对促进经济社会发展和生态改善都有好处。要在科学规划的基础上，加快建设。<br />
<br />
除了拦水、蓄水外，打井也是增加水源的重要途径。石灰岩地区，水并没有完全跑掉，有的进入了地下水库、地下溶洞、地下暗河，要摸清石灰岩地区地下水分布状况。水利、地质等部门，要派出强有力的技术队伍，进行地下水勘查，特别要寻找蓄水量比较大的地下水源。在喀斯特地貌区，打一口井，干旱时就能解决大问题。<br />
<br />
二是加强小型水利设施建设。这次旱灾暴露了农田水利设施薄弱的问题，也激发了农民水利建设的积极性和主动性。我觉得这是一个动员农民的好机会。因为通过这次旱灾，灾区的农民有切肤之痛，他们的感受最深。要抓住这个有利时机，加强政策支持，通过以奖代补、民办公助等形式，鼓励农民大力兴办小型水利设施，把农民的积极性保护好、发挥好。要优先安排农民群众要求最迫切、最急需、效益最显著的工程，加快兴建集雨水窖、泵站提水、集中供水等农村饮水安全工程，加快建设小山塘、小电灌站等“五小水利”工程。<br />
<br />
三是加强节水工程建设。要全方位加大节水力度，大力推广节水技术。加快大型灌区节水改造，鼓励农民发展集雨节灌等。把节水灌溉作为一项根本性措施来抓，采取切实措施，不断创新节水机制和节水模式。<br />
<br />
这次特大旱灾，对各级党委、政府都是一个考验，对干部也是一个考验。我们一定要尽最大努力，确保人民群众的基本生活，确保经济社会的健康发展。我相信，我们一定能够做到这一点。</div>

							
							
							
							
