													<h2>「我们不能喝你的水」</h2>
						
						
																											<div id="postmessage_535727" class="t_msgfont">O果日缶W上版 －－2010-03-27<br />
「我们不能喝你的水」<br />
<br />
中国西南十多个省分最近旱魃成灾，赤地万里。中共总理温家宝亲到云南灾区，精心安排到一户民家探访，受访者要给他倒茶，温家宝连忙阻止说：「我们不能喝你的水，运来一点水不容易。」这大概是中共的爱民方式。我国传统的爱民官，不会这样拒民一杯水。<br />
<br />
宋朝名臣马遵曾任宣州太守，多行惠政，一地足食丰衣。后来马遵去职，百姓盛意挽留，甚至「以铁锁绝江」，教他不能成行，并设宴劝他续任。马遵欣然出席，饱食醉饮，一副恋恋不去的样子，大家都以为他会留下来。到了深夜，马遵使人悄悄打开江锁，解舟而去。有与民同乐的官，怎会有连水都几乎没得喝的百姓（《临汉隐居诗话》）。<br />
<br />
明朝成化年间，王恕奉命巡抚云南，不携僮仆，只带 灶、竹食箩各一个，沿途「酱、醋、水皆取主家」，其余一切自给。他有告示说：「欲携家僮随行，恐致子民嗟怨，是以不恤衰老，单身自来。」百姓都为他焚香礼敬（《玉堂丛语》卷五）。温家宝则不同，云南一行，随身秘密警察不知凡几；所经之处，警 卫森严，百姓止步。而他以为「我们不能喝你的水」就等于爱民。<br />
<br />
大旱固然是天灾，但二○○四年，美国五角大厦已经预测华南二○一○年前后会有大旱，中共《人民日报》也说温家宝政府对这预测「非常重视」。现在六年过去了。防旱工作原来丝毫没有。当局对百姓的唯一责任就是征税。例如几年前广西东兰县村民交了修路费，今逢大旱，却还是无路可行，每天得翻山越岭走二十多公里，挑水回家。<br />
<br />
同时，我们看见中共为求金子银子，四处移山竭泽，浑沌的大自然给他们昼夜穿凿，已告体无完肤。二○○四年七月九日《中国青年报》载：印度尼西亚金光集团和中共官方合作，大肆砍伐云南、广西等地原生林木，改种生长迅速的桉树，以便造纸，「无数桉树有如一台台抽水机，吸干了地下水」。浙江摄影师卢广一辑《中国的污染》相片，赢得美国人道主义纪实摄影奖之余，也拍出了全国江河土地怎样不敌中共的经济发展，或干涸荒芜，或变成污水毒壤，于是癌症村、铅毒村、畸形儿童村等新闻无日无之。<br />
<br />
古人说，为政必须「燮理阴阳」，而燮理阴阳的办法，不外顺天应人，为政以德。这不完全是迷信。你看今天新中国，年年不是「五十年一见的洪水」，就是「一百年一见的大旱」、「千百年不一见的沙尘暴」等等。这一切都和为政之道大有关系，而温家宝对这一切的答案是：「我们不能喝你的水。」<br />
<br />
从前，我国清官不会介意喝百姓的水。他们只是不吃百姓的膏血。<br />
<br />
古德明<br />
