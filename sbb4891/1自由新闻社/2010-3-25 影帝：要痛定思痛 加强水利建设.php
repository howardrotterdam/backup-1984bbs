																											<div id="postmessage_537300" class="t_msgfont"><div class="quote"><h5>引用:</h5><blockquote>原帖由 <i>二爷</i> 于 2010-3-27 23:00 发表 <a href="https://1984bbs.com/redirect.php?goto=findpost&amp;pid=535637&amp;ptid=41347" target="_blank"><img src="https://1984bbs.com/images/common/back.gif" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /></a><br />
水利从古代就是国家最着急修建和修缮的工程之一，甚至比军队还要棘手。<br />
<br />
什么叫痛定思痛兴修水利，和着除了破坏水生态的各种大坝以外，最近几年真正的水利就没修过么。 </blockquote></div>　　其实，只要浏览一下国家气候中心和西南各地气象科学家摆出的数据，了解西南大旱的成因，这一点并不困难。<br />
　　目前，比较普遍的结论，认为青藏高原较强的热力因素、去年过早结束的雨季、厄尔尼诺现象间接影响影响下由南方进入到西南的水汽偏少，以及冷空气偏向难以形成有助于将于的“水汽”，这些都是影响西南旱灾的重要原因，而众多原因凑在一起，则直接导致了此次旱情的严重。<br />
&nbsp; &nbsp; 水电又一次成为了替罪羊。“西南地区的水电站建设密集上马”、“上游对江水的蓄积让下游的河道干枯” 也可能是大旱的一个原因等说法也被媒体频繁引用。这样的说法除了无法与上述的气象数据相联系外，也忘记了西南地区的水电其实大部分还在规划、建设甚至是争议之中，即使它们真如诸多的反对意见所声称的具有破坏性，这种破坏性还远不足以构成对气候条件的影响。<br />
　　而与之相反，在西南旱灾中暴露出来的另一个被国家决策者认识到的问题，却被一些“环保志愿者”有意无意地忽视，那就是目前的西南地区普遍缺乏完善的蓄水水利措施，这直接导致了旱灾之中无法合理利用有限的水资源。<br />
　　事实上，在许多水利资源利用充分的发达国家，如水利资源利用率高达90%的挪威，并没有看到水利工程对灾难性天气的促力作用，相反，管理完善的蓄水水利措施，却很明显地在缓解旱情影响（与水坝不能造成大范围长时段极端天气事件一样，它们也不能直接缓解旱情而只是通过对水资源的调节来减轻其影响）和提供应急资源，特别是电力方面发挥了明显的作用。<br />
　　尽管旱灾让西南已经投产的水电企业普遍发电量大幅度减少，股价下跌，但是它们仍然为抗旱救灾提供着珍贵的电力。试想一下，如果没有这些电力，抗旱的压力是更大呢还是更小？<br />
　　也许有人会说，可以利用其它能源发电。但是在中国用不起油、气而风能太阳能等新能源远远不够成熟的情况下，其他能源就只剩下煤炭。而科学分析中的大旱成因，恰恰与碳排放增加导致的全球变暖密不可分。在这种意义上甚至可以说，水电（如果还有核能的话）是减轻而不是加大旱灾的因素。<br />
　　当然，对水电这种低碳排放的能源的全面效应，还需要进行更加全面的生态和经济层面的研究，这正如对待西南大旱的成因一样。唯有这样，才是科学的态度。<br />
