													<h2>央视人事大变局</h2>
						
						
																											<div id="postmessage_808552" class="t_msgfont">央视人事大变局<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;文/&lt;&lt;中国广播影视》记者 温 静<br />
&nbsp; &nbsp; 干部选拔任用制度改革与频道制改革唱上和声。<br />
<br />
&nbsp; &nbsp; 将竞争上岗作为基本制度，调整一批，交流一批，竞聘一批，多项举措同时进行。<br />
<br />
&nbsp; &nbsp; 职务从高到低竞争上岗，先定将，再找兵。<br />
<br />
&nbsp; &nbsp; 5月12日，中央电视台启动首轮大规模领导干部竞争上岗，与“蓄谋已久”的频道制改革和声相伴。<br />
&nbsp; &nbsp; 此后的两个半月，央视人事有较大幅度的挪移，据《中国广播影视》了解，其内部产生了强烈的震动。<br />
&nbsp; &nbsp; 7月27日，央视召开“竞争上岗总结大会”。尽管天气潮闷，但会场气氛却很严肃。中央电视台副台长张长明在会上公布了这样一组数字：四轮竞争上岗，公布的职位数有102个，近429人报名，99人走上局、处级的领导岗位，占到央视现有干部总数的29%，其中17名企聘员工走上了副处级领导岗位。同时聘任了 18名专职副书记，强化了党组织的人才配备。<br />
&nbsp; &nbsp; 中央电视台台长焦利则在大会上宣布了改革的时间节点，以这种紧迫感推进下一阶段的任务目标――从7月27日开始至8月底，是由中心制到频道制的过渡期。8 月1日前各频道的栏目、人员、资金及物资要分割完毕；8月份，频道及中心要按新体制运转；9月1日开始，各频道进一步改进和提高节目质量的各项措施要陆续出台，“年底前，所有频道都要亮起来”。<br />
&nbsp; &nbsp; 第一轮副局级干部竞争上岗后，焦利在全台例会上说，没有一个人为此找过他。<br />
&nbsp; &nbsp; 总结大会上，他对“服从大局、听从安排”的干部提出了表扬，他认为此次央视领导干部竞争上岗体现出了很强的组织原则和个人修养。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;<br />
有了“身份”<br />
&nbsp; &nbsp; 8月10日，段晓超轻轻地推开了会场的门，他是作为企聘员工代表来参加广电总局在央视召开的“干部竞争上岗工作调研会”的。20多天前，正是在这个会场，段晓超完成了他人生中第一次竞争上岗演讲答辩。推门的一刹那，他别有一番感触。<br />
&nbsp; &nbsp; 当第一轮竞争上岗开始的时候，“我根本没有想到竞争上岗这事能和自己有什么关系。”同段晓超一样，很多人都在观望，也心存疑虑。<br />
&nbsp; &nbsp; 十年前，段晓超还是26岁的大学毕业生时，就在央视社教中心一线工作了。如今他早已过了而立之年，却因身份问题而“不能再进一步地发展”，为此颇感苦恼。<br />
&nbsp; &nbsp; 央视有企聘员工6000多人，台聘员工2000多人。改革前，在中央电视台的管理体系中，“人员身份”是一道迈不过去的“坎”，企聘员工最多只能做到制片人一级。<br />
&nbsp; &nbsp; 当第三轮竞争上岗展开的时候，一些领导开始鼓励段晓超：“小段，要好好准备一下，要抓住机会！”“可我是企聘人员。”这是他当时的第一反应。<br />
&nbsp; &nbsp; 段晓超说，“说实在的，我们很多企聘员工在目前的岗位上是骨干，但仅仅是由于身份问题，一直没有机会参与干部选拔。”<br />
&nbsp; &nbsp; 7月5日，央视启动了频道总监助理、部门副主任等43个副处级岗位的竞争上岗工作。“当确定企聘员工真的可以参加竞聘时，我身边够条件参加的几乎都报名了。”段晓超说。<br />
&nbsp; &nbsp; 记者了解到，企聘员工参与干部竞聘上岗需要满足以下条件：年龄在50周岁以下（含50周岁）的现任科组长、制片人，由所属中心（室、频道）确认的现承担科组长、制片人工作的人员；具有5年以上专业工作经验。<br />
&nbsp; &nbsp; 另外，符合下列条件之一的也可参与竞聘：曾参加援疆、援藏挂职锻炼（时间半年以上）；近5年内获得国家级、广电总局系统重要奖项的主要贡献者，获得国家级荣誉称号、被选为全国宣传文化系统“四个一批”人才，央视优秀播音员、主持人，获得央视颁发的各类节目一等奖的主要贡献者以及青年五四奖章（十优青年）获得者。<br />
&nbsp; &nbsp; 第四轮竞争上岗成为涉及面最广、参与人数最多的一轮。记者从央视人事办公室获悉，有301人报名参加竞聘，平均1个岗位8人竞争。这其中有一个有意思的细节，段晓超告诉记者，“大家都想拖到最后一刻报名，想先看看不同职务的竞争情况，有点像高考填报志愿。”<br />
&nbsp; &nbsp; 最终有118名企聘员工报名，占报名总人数的39%。各中心（频道）负责人共322人次作为专家评委，960人次干部群众评委参加了32场演讲答辩会的评审。<br />
&nbsp; &nbsp; 段晓超之前的岗位是社教中心综合部秘书组组长，最终竞聘了社会与法频道（CCTV-12）综合部副主任。“也有领导劝我竞聘节目部副主任，可以发挥我频道改版、评估等方面的经验，最后的报名结果也是思考再三。”他说，“这次改革，不仅‘把首次提名权交给了自己’，而且力求做到‘人岗相适’、‘双向选择’。”――候选人可以在全台范围，根据自己的特长、经验、兴趣选择相应的竞争岗位。<br />
&nbsp; &nbsp; 经过初评、终评两轮竞争，有17名企聘员工被任命为副处级干部，占第四轮新增干部数的40%。段晓超是参与者，也是受益者。当他的任命还在公示期的时候，有位领导对他说，“如果还是在老体制下，要走到这个位置，也许你至少还需要十年。”<br />
&nbsp; &nbsp; 据了解，央视领导干部竞争上岗也使干部队伍结构进一步优化，第四轮竞争上岗产生的干部，平均年龄39.3岁，有7个人年龄在35岁以下，还有一名80后女性，作为年龄最小的干部被聘任为少儿频道综合部副主任。第四轮中，共有13名女性干部走上了领导岗位，占31%。<br />
&nbsp; &nbsp; 有人说，央视存在着干部队伍老化的现象，而且台聘、企聘两种用工制度的界限，客观上使选人用人的视野受到了局限。<br />
&nbsp; &nbsp; 主持人事工作的副台长张长明这样评价，“这次干部竞争上岗是央视以深化内部改革为契机实施的，在第四轮竞争上岗中，所有符合条件的企聘员工都可以参加。多年积累的用人制度的界限被打破了！”<br />
&nbsp; &nbsp; 张长明称此次央视领导干部竞争上岗为“革命性的突破。”<br />
<br />
<br />
人事变阵<br />
&nbsp; &nbsp; 中央电视台台长焦利总结这次的干部和人事调整，认为具备涉及范围广、调整难度大、参与程度高、工作极其复杂的特点。<br />
&nbsp; &nbsp; 参与各个岗位竞争的有429个人，涵盖了央视所有节目部门、党务部门、直属单位，此间还面临着实行频道制后的新情况，有的部门被分割、有的重组、有的新建。改革和正常宣传工作同时推进中，很多干部还要兼顾新老岗位。<br />
&nbsp; &nbsp; 此次干部选拔任用制度改革，将竞争上岗作为基本制度，采用竞争上岗、任命、调剂相结合的方式。“调整一批，交流一批，竞聘一批，多项举措同时进行。”焦利说。<br />
&nbsp; &nbsp; 换句话说，职务从高到低竞聘，先定将，再找兵。<br />
&nbsp; &nbsp; 竞争上岗面向全台和直属单位，而且干部交流也在台本部和直属单位之间、事业和企业之间进行。人事办公室、财经办公室以及社教中心的领导干部就交流到了新影、科影和总公司等直属单位，同时直属单位也有人通过竞聘、交流等方式到了台本部工作。<br />
&nbsp; &nbsp; 5月12日启动第一轮竞争上岗，央视拿出了包括频道总监、常务副总监以及中心常务副主任、中心副主任等在内的12个副局级岗位，32人报名参加，最终产生了11名干部。如原中国电视剧制作中心副主任张子扬任电视剧频道总监。<br />
&nbsp; &nbsp; 6月7日启动了频道副总监和中心副主任等27个职务的第二轮竞争上岗，59人报名参与，产生干部26人。如郑蔚任财经频道（CCTV-2）副总监。<br />
&nbsp; &nbsp; 6月24日，频道综合部、节目部主任以及新闻中心、节目采购中心、广告经营管理中心所属部门主任等20个职位的第三轮竞争上岗工作开始，报名37人，干部如数产生。如李瑞英任新闻中心新闻播音部主任。<br />
&nbsp; &nbsp; 尤其值得关注的是第四轮。这一轮，有更多观众熟悉的名字出现。主持人中，康辉任新闻中心新闻播音部副主任，王世林任新闻中心国际频道新闻编辑部副主任，马东任文艺频道（CCTV-3）总监助理。2011年央视春晚，马东将进入导演组，负责语言类节目。<br />
&nbsp; &nbsp; 李瑞英此次由副处提升为正处为破格任用。这样的例子还有原新闻中心人事办主任王晓真，他竞聘新闻中心副主任成功后，又被调任为央视人事办公室主任。这次改革共有9名公认较为优秀的干部被破格提拔。<br />
&nbsp; &nbsp; “求贤若渴的心情形成了自上而下的改革形态。”一位央视人讲到。<br />
<br />
比高考还严<br />
&nbsp; &nbsp; 竞争上岗，人们最关注的在于是否真能做到公开、公平、公正，议论最多的则是竞争选拔机制是否科学、规范、严谨。<br />
&nbsp; &nbsp; 段晓超在这次竞争上岗中，既竞聘过，又做过干部职工评委。他在讲述时强调一个词――“动真格的！”按照他的讲述，这次改革所有的程序、环节都公开透明，没有暗箱操作，更不是走过场。<br />
&nbsp; &nbsp; 从报名、资格审查到公布竞聘人员名单、竞争上岗，“台里一般只给三天，最多一个星期。基本上报名第二天就会通知能否参加了。”<br />
&nbsp; &nbsp; 他参加的第四轮竞争上岗，由于报名人数太多，不同于前三轮，分了初评和复评。<br />
&nbsp; &nbsp; “演讲答辩”在这四轮也有同与不同。相同的是每一轮的规则都一样，分为“个人演讲”和“回答问题”两个部分；不同的是时间，第一轮竞聘者演讲答辩时间为20分钟，第二、第三轮为15分钟，第四轮缩减为10分钟。<br />
&nbsp; &nbsp; 对于竞争同一岗位的人员，为了便于横向比较，设置的考题是一样的。央视人事办相关负责人告诉记者，竞聘程序是央视设计的，命题则完全用的是外脑，他们专门请了富有经验的国家行政学院来命题。<br />
&nbsp; &nbsp; “不是他们出什么题，我们就用什么题。”人事办相关负责人解释说，首先要让国家行政学院的出题人对央视的改革情况有所了解，对于频道、栏目的详细情况，比如收视率、收视人群、节目定位等有一定把握，然后再就大概的出题内容与主管副台长沟通。<br />
&nbsp; &nbsp; 此次考试甚至比高考还要严格。命题结束后，央视人事办、保卫处、监察室三个部门各出一人，去国家行政学院取回试题，直接送到保密室封存，直到考试开始前 10分钟，才由人事办、监察室各出一人去保密室取题。之后试题一直在监察室工作人员的手里。主考官宣布开封后，由监察室工作人员开封，向评委发放试题。<br />
&nbsp; &nbsp; 央视专门设置了候考室，实行严格的封闭候考，相当于把所有候考人都“关”起来，手机上交，室内的电话、电脑拆除，以保证他们不和外界联系。<br />
&nbsp; &nbsp; 段晓超印证了人事办的说法。“有一男一女两个工作人员负责监督候考人，连上厕所都要陪着去。还有专门的引导员，我们去考场和答辩完离开考场，路线都不一样。”<br />
&nbsp; &nbsp; 候考人通过抽签决定出场顺序。评委分为“专家评委”和“干部职工评委”两类。干部职工评委都是通过电脑随机抽取出来的，比例是部门领导40%、科级干部 40%、员工20%。技术人员为此设计了专门程序。这一过程也由央视纪检组监察室全程监控。“评委有的我熟悉，更多的还是陌生人。”段晓超告诉记者。<br />
&nbsp; &nbsp; 为最大限度地减少拉票贿选行为，抽取出干部职工评委后，要等晚上他下班离台后再通知。那段时间，人事办基本都是晚上十一二点下班。被抽到的人第二天一早到考场签到，核对身份后进入考场。<br />
&nbsp; &nbsp; 据了解，这次的竞聘命题，从岗位的工作实际出发，又高于目前的岗位工作要求，要求竞聘者“跳一跳，摘到桃子”。<br />
&nbsp; &nbsp; 在分值设置方面也下了功夫。分数在90～99分之间，高于99分或低于90分的算无效票，95分以下的算不及格。采用这种“小分差”的评分体系，是为了防止其中一位评委有意打出低分就能把竞聘者的成绩拉下来。<br />
&nbsp; &nbsp; 专家评委权重占70%，干部职工评委权重占30%。评委全部实名打分，如有异常分数，人事办还要进行核实。<br />
打分结束后，由电脑按照权重自动生成分数，当场就向专家评委公布成绩。“我记得，答辩完没过多久我就收到了祝贺的短信。”段晓超说，“有的竞争者之间的差距，仅仅只有0.1分。但‘真改革’和‘走过场’的距离，也许恰恰就是这0.1分。”<br />
&nbsp; &nbsp; 这种考试，会不会让语言表达能力弱的人吃亏？<br />
&nbsp; &nbsp; “我们也要防止能说会道的人占便宜。分党组是决策机构，会通盘考虑‘德能勤绩廉’各项指标。考核的过程中还是要有调控，其中几个人都进行了调整。”人事办相关负责人说。<br />
<br />
下一站，频道制<br />
<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp; 此次改革，央视所有干部的岗位，都是按照频道制的管理体制配置的。四轮竞争上岗的结束，标志着央视以频道制为主的管理体制的形成。焦利在总结会上指出，“能否科学合理地建立新的频道责任主体，是改革的关键。”<br />
&nbsp; &nbsp; 此次改革，央视新建了5个中心――海外传播发展中心、大型节目制作中心、节目采购中心、广告经营管理中心、发展研究中心；还有6个频道涉及重组和新建，包括综合频道、文艺频道、戏曲・音乐频道、电视剧频道、科教频道、社会与法频道。<br />
&nbsp; &nbsp; 实行频道制后，对频道专业化和集中垂直化的管理要求更高，管理层级大幅减少。频道下取消了原中心制“中心―部门―科组、栏目”的管理层次；实行了目前扁平化的两级管理架构：总监（常务副总监、副总监）―栏目制片人；总监（常务副总监、副总监）―部门主任（副主任）。<br />
&nbsp; &nbsp; 频道下仅保留了节目部、综合部，总监授权副总监对各栏目负责，总监（副总监）直接管理节目部和综合部。<br />
&nbsp; &nbsp; 央视几年前就开始在体育频道和少儿频道进行频道制试点，之后组建的财经频道也采用了这一体制。<br />
&nbsp; &nbsp; “建立科学高效的频道制管理体制和运行机制，既是实践问题，也是个理论问题，需要不断地探索、总结和完善。”焦利指出。<br />
&nbsp; &nbsp; 在频道制改革中，央视确定了一个原则，“就是栏目跟着频道走，人员、经费和物资跟着栏目走，首先进行物理分割，以确定各项工作尽快到位。”焦利在总结会上特别提到了文艺节目中心和社教中心的改革。<br />
&nbsp; &nbsp; 文艺节目中心“频道制”后，一分为五――三个频道重组，两个中心新建：分别是文艺频道（CCTV-3）、电视剧频道（CCTV-8）、戏曲・音乐频道、大型节目制作中心、节目采购中心。“工作很复杂。我听了一次他们的工作安排，重组和新建工作井井有条，扎实有序，各个部门的分割工作也基本到位。”焦利说。<br />
&nbsp; &nbsp; 8月9日，中央电视台副台长胡恩在电视剧频道成立大会上说，该频道的成立是央视频道制改革的一项重要成果。电视剧频道提出，要将明确频道定位、细化改版方案等作为下一步工作的方向。<br />
&nbsp; &nbsp; 原社教中心一分为二，现在分为了央视科教频道（CCTV-10）和社会与法频道（CCTV-12)。社教中心“创造性”地成立了频道制切分运行领导小组，制定了切分工作路线图和时间表，提出切分、建制、改版三项重点任务和四个阶段的工作措施，“工作细致周到”，焦利评价。<br />
&nbsp; &nbsp; 8月1日，科教频道实行频道制后，制定了“三项制度”：周一频道办公会，搭建制片人直接面对总监发言、建议、咨询的平台；周四频道节目会，传达落实宣传指示精神和节目编播管理；每周二设立综合部、节目部联席会，搭建共同运行平台，服务栏目生产一线。<br />
&nbsp; &nbsp; 焦利指出，从7月27日总结大会当日到8月底，是中心制向频道制的过渡期。7月底之前，各频道的栏目、人员、资金及物资要分割完毕。<br />
&nbsp; &nbsp; 合理分割，平稳过渡，需要所有部门提供保障和服务。<br />
&nbsp; &nbsp; 焦利对其他部门提出了明确、细致的要求：总编室要根据重组和新建部门节目生产和实际工作量，合理地划分节目经费和日常工作运转经费；人事办要按照新体制和机构设置，抓紧制定相关岗位工作职责，尽快办理相关人员的进行划转手续；财经办要按照频道制管理体制，进一步完善财务管理制度，做好体制转换期间工作、奖金发放、财务报批等各环节的衔接工作；技术系统要按照频道制要求，为节目部门提供技术支撑和物资保障；台办公室要统筹房源，合理调剂余缺，尽快落实新组建部门的办公场所等。<br />
&nbsp; &nbsp; 在体制调整转换期间，“把握导向、守土有责是首要任务！”焦利要求，“所有分管台领导都要靠前指挥，亲自抓部署，抓落实。”<br />
&nbsp; &nbsp; 实行频道制后，各频道完善工作流程，要建立相关制度。“首要的是，尽快建立起节目审看和把关制度。”焦利强调，“频道制仍然要坚持节目的‘三审’制度，频道总监和副总监负责节目的终审，重大宣传项目和重点节目，分管台领导要亲自把关。要把坚持正确的舆论导向贯穿每个频道的每个环节，不允许出现调整期间有岗无人、有职无责的状况。”<br />
&nbsp; &nbsp; 焦利认为，央视在频道制运行初期，要着重关注几个问题：<br />
&nbsp; &nbsp; 围绕一个核心――推进频道专业化。目前，央视有20多个开路频道，实行频道制后，进一步明确了频道是节目制作和生产的责任主体，频道的责任意识、管理能力、资源的整合和运用能力、对市场的应变能力都增强了。这些都会有力地推进频道的专业化建设。实行频道制，最终是要提升频道的品质、竞争力和影响力。<br />
&nbsp; &nbsp; “要按照专业频道的定位，精心设计栏目和节目，彻底解决为了提高收视去打擦边球，制作不符合频道定位的栏目和节目；解决为了提高收视率，都去播电视剧、都去搞娱乐节目的泛娱乐化倾向。”焦利强调。<br />
&nbsp; &nbsp; 突出一个重点――进一步强化频道内部管理。频道制减少了管理层次，减化了机构和人员，这些都对加强频道内部管理提出了新的要求。央视人事办负责人告诉记者，“台里正在研究制订新的‘五定’方案――定机构、定职责、定编制、定人员、定薪酬，力争尽快搭建起频道内部最基础的管理架构，规范各频道（中心）的内设机构和用工。”<br />
&nbsp; &nbsp; 记者了解到，包括财经频道（CCTV-2）等多个频道的制片人竞争上岗已经展开。“这次制片人竞争上岗，根据‘五定’，要杜绝‘内部粮票’（即五花八门的自设职务名称），台里认可，人事办发文才认（这个职务）。”人事办相关负责人表示。<br />
&nbsp; &nbsp; 抓住一个关键――频道的品牌栏目建设。“提高节目质量、打造品牌栏目是频道制的追求，也是频道制改革成功与否的标志。”焦利指出，按照央视的电视宣传格局和总体考虑，把综合频道（CCTV-1）定位为以新闻宣传为主的精品频道，要求所有的栏目和节目都要办成精品。<br />
&nbsp; &nbsp; “对于其他频道，我们没有这个要求，这样要求也做不到，也不可能。但是我们要求每个频道都要有几档能代表频道品质、收视表现较好、具有一定影响力的品牌节目。”央视今年特别拿出了一个亿作为栏目创新专项资金。<br />
&nbsp; &nbsp; 对于央视的频道制改革，议论很多。一位网民的话引起了焦利的注意――“我们只关心电视节目，至于电视台用什么形式我不看重。”<br />
&nbsp; &nbsp; “我认为他说出了最实质性的东西。”焦利说。<br />
