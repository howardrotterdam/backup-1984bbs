远没有结束<br />
&nbsp; &nbsp; 2009年5月焦利走马上任中央电视台台长。现年55岁的焦利，记者出身，历任《辽宁日报》社长、辽宁省委常委、宣传部长，2008年9月～2009年5 月任中宣部副部长，来到央视后即强调“新闻立台”的发展方向，整合新闻、广经、海外、社教、文艺五大节目中心的新闻采编系统，成立新闻中心。<br />
&nbsp; &nbsp; 他在“竞争上岗总结大会”上指出，还要继续加大新闻中心的改革，巩固新闻共享成果，进一步放大资源整合的效应。<br />
&nbsp; &nbsp; 央视改革的目标，放到国际传播大格局中看也许会更加清晰。实际上自2008年开始，我国启动了一个庞大的媒体计划，以扩大国际舆论影响力、改善国际形象、提升文化软实力。2009年，央视新闻频道改版，增设俄语及阿拉伯语频道，计划在3年内实现7种语言、11个频道的国际化布局。2009年12月28日，央视正式推出了“中国网络电视台”，以建成全球化、多语种、多终端的网络视频公共服务平台为目标。<br />
&nbsp; &nbsp; 几乎同时，2009年12月31日，新华社主办的中国新华新闻电视网也举办了开播仪式，并于2010年1月1日上星播出。<br />
&nbsp; &nbsp; 2010年是央视加强国际传播的起步之年，央视设立海外传播发展中心，将把CCTV-9在海外的落地作为重点，并提出了今年底海外记者站的数量要达到50个，同时要建七大中心站，国内31个省区市的应急报道点要全部配齐。<br />
&nbsp; &nbsp; 焦利在辽宁省主管宣传工作时，一手将辽宁出版集团运作成为了国内第一家上市的出版社，文化体制改革成绩斐然。他认为，“近年来，央视产业发展很快，今年上半年，广告形势也很好。这是我们自己与自己比，如果横向比较，我们觉得还有不小的差距。”<br />
&nbsp; &nbsp; 他坦陈，一是我们的思想还不够解放，还缺乏加快发展文化产业的紧迫感；二是视野还不够开阔，没能有效地引进战略合作伙伴；三是资本市场利用的不够好，不具备实现跨越式发展的条件。“实际上，央视有丰富的文化产业资源和独特的优势，在电视行业的任何一个领域，都有做大做强的条件。有人说，这个领域正面临着一次重新洗牌，如果是这样，我们更应该抓住机遇，加快发展啊！”<br />
&nbsp; &nbsp; 这次改革，央视新成立了发展研究中心，就是要把产业发展作为一个重大课题，规划好当前及未来5年的发展规划。“这个中心将成为央视的智库。”一位业内人推测。<br />
&nbsp; &nbsp; 改革还要体现效果，“如剧中心的改革，要尽快把事转企释放出的能量转化到节目的制作和生产上。”焦利说，“我们考虑，在完善频道制改革的基础上，在某些部门，要积极稳妥地进行制播分离改革，充分发挥体制上的优越性，进一步解放文化生产力。”<br />
