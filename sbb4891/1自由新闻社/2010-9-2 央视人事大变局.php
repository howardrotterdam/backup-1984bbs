&nbsp; &nbsp; 面对一大批走上了各级领导岗位的优秀干部，焦利有很多话要讲。“今年是我台以队伍建设为主要内容的形象建设年。竞争上岗，为我台事业发展提供了有力的人才支撑和组织保障。我要说的是另外一个问题――”<br />
