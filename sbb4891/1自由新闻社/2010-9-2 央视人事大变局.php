&nbsp; &nbsp; “央视不是行政机关，可以在那层层发号召、作指示，我们是生产单位，要为亿万电视观众提供精神文化产品，领导同志都要以自己的模范行动来带动大家。我听说有的同志担任一定的职务后，就开始当脱产干部，甚至有的制片人都脱产，虽然是个别现象，但要引起我们的警惕。<br />
