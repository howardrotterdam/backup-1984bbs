<br />
“四o一二”以后，就由吴稚晖出面提议“清党”，并在南京另立“政府”与“中央党部”，与武汉分裂。五六月间蒋介石派“清党”的要员杨虎、陈群两人（上海人民叫他们为“狼虎成群”）到宁波去“清党”，因为宁波是蒋介石的家乡，所以杨虎、陈群特别向蒋请示方针，蒋介石对他们说:“凡是可以杀的一律杀。宁可错杀，不可错放！我们将来终是要给人杀的，不如多杀几个。”蒋介石自己知道是一个纸老虎，所以只能以残杀来壮胆逞威，“唯懦弱者最残忍”，的确是至理名言。<br />
