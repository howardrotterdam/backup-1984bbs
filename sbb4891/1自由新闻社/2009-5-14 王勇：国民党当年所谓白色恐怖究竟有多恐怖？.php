													<h2>二、民权与民风，限制了国民党胡作非为的手脚</h2>
						
						
																											<div id="postmessage_92715" class="t_msgfont">国民党既然要以三民主义为宗旨，那就得讲讲民权，有所顾忌，不能想干什么就干什么。<br />
<br />
看过革命人物回忆录的读者，就会发现，相当多的中共党员都是“几进宫”，因为没证据，或者要根据事实来量刑，所以被国民党放了又抓，抓了又放。除了说明共产党人坚贞不屈的高尚品德以外，也说明国民党当局在镇压革命的过程中没有完全抛弃司法程序，而悍然实行“宁可错杀三千”的残暴政策。<br />
<br />
比如，中共元老薄一波先后4次被捕。第1次是在1929年的唐山，因身份不明被被押进警备司令部，但由于没有证据，4个小时后即被释放。第2次是在1930年的天津，因策动兵变，判刑1年零3个月。第3次是在1931年的北京，他去一个饭店接头，刚进门就被卧底的便衣抓住，但他一口咬定是去吃饭的，警察没有任何证据，在把他关了59天之后只好放人。随后不久，到这年6月，时任中共河北省军委常委的他被叛徒出卖，这便是薄一波的第4次被捕。<br />
<br />
又比如，从中共党员李逸民的回忆中我们得知，1928年，就在号称“人间地狱”的国民党龙华警备司令部，也是因为没有证据，只得放走中共南京市委书记。<br />
<br />
他这样回忆道:<br />
<br />
在我房里的那位小士兵释放了，又关进一个三十多岁的男子，带着八百度近视眼镜。身体很瘦，年纪不大，头发已经白了不少，一看就知道，这是位政治犯。他带着手铐脚镣是从南京送来的，我问他半天，他只说是一个小学教员，至于什么地方人，在哪儿念过书，做过什么工他一概不谈。他问我的情况，我把原来的口供向他叙述一追，他批评了我说:“你说的太多了。”这时，我明白，他大概是一个很老练的共产党员，从此以后我们什么也不谈了，他爱下棋，我们就整天下棋。后来，我打听到，他是南京市委一个书记。因敌人抓不住他任何证据，终于释放了。释放那天，他和我们一一告别，还送我一双手套。</div>

							
							
							
							
