<br />
不过，史料看多了，才慢慢明白，原来这些印象大都来自于小说和电影，而在很多时候、很多地方，国民政府的统治并不严厉，“白色恐怖”并非常态，即使是在所谓“特务密布，军警林立，控制最为森严”的上海滩，马列主义的著作也是可以公开出版的。<br />
