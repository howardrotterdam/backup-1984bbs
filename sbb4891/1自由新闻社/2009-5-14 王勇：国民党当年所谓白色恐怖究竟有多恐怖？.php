<br />
长期以来，很多人都以讹传讹地认为，蒋介石下令实行了“宁可错杀三千，不可放走一人”的屠杀方针。团结出版社2004年出版的《陈立夫大传》便如此记述:“蒋介石在1927年四一二反革命政变后，对于逮捕的共产党员和进步人士采取'宁可错杀三千，不可放走一人'的残暴政策，大肆屠杀。”解放军出版社1997年出版的纪实文学《穿过硝烟的握手》似乎也证实了这一说法，文中在关于蒋介石与苏联大使商谈第二次国共合作、一致抗日的问题时这样写到，蒋介石表示可以让中国共产党合法存在，“语气那么大度与肯定，仿佛对共产党举起屠刀，咬牙切齿喊出'宁可错杀三千，不可放走一人'口号的不是他而是别人。”<br />
<br />
其实，“宁可错杀三千，不可放走一人”这句话，并不是蒋委员长说的，而是出自于桂系军阀陶屠户之嘴。</div>

							
							
							
							
