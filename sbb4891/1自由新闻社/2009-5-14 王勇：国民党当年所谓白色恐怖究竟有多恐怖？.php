1927年4月10日，宁波《民国日报》以社论历数当地驻军司令官王俊的十大罪状，同时还刊发一篇《蒋介石尤效军阀故智耶》》的新闻，这下让对方抓住把柄，思想左倾的社长庄禹梅因此下了大狱。4月12日，他在狱中看到捉来一群年轻人，原来当天上午，各界群众举行示威游行，工人纠察队手持棍棒冲在最前列，与军警发生激烈冲突，两人被击毙。<br />
<br />
6月22日，陈群、杨虎一到宁波就开始行刑逼供，共产党员王鲲、杨眉山都挨了皮鞭。庄禹梅因为还不是共产党员，当然不承认，所以被打得更重，“鞭子就像雨点一般落下来，这样的接连打了七次，背上已经血肉模糊，我终于没有供认”。<br />
<br />
为营救庄禹梅，有人将他以前所写一部反映孙文革命事迹的《中山演义》交给了军法官。那天，当他正在受刑时，陈群问他:“你信仰三民主义有何根据？”他答以写过《中山演义》一书。陈群把书翻了翻，又问作者怎么叫“庄病骸”？他说:“病骸是我的笔名，作者就是我。”于是，陈群便停止审问。<br />
<br />
当天下午，王鲲、杨眉山被斩首。<br />
<br />
第二天和第三天开庭中，有的释放，有的交保，共产党员甘汉光、胡焦琴（女）和陈良义、吴德元先后牺牲。<br />
<br />
到第四天，在当地士绅的请求下，杨虎、陈群才罢手，将剩下的19人送进监狱。庄禹梅虽被判刑10年，但不到一年，便在商界名流虞洽卿的疏通下无罪释放。<br />
<br />
通过庄禹梅的记述，我们得知，被关押的100多人中，大部分都被释放，可见所谓“宁可错杀，不可错放”的话又有多少可信度？</div>

							
							
							
							
