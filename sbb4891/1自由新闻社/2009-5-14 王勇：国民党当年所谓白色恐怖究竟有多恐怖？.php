																											<div id="postmessage_92716" class="t_msgfont">再举一例:<br />
<br />
杭州中心市委组织部部长的徐迈进于1929年被捕，被送进江苏陆军军人监狱。有天，他给狱外党组织写密信被发现，敌人追问半天无结果，就给他上了一副15斤重的脚镣，还把他解到南京的军政部军法司进行审讯。<br />
<br />
没想到他从《陆海空军审判法》中发现-条“受军法审判的要有军人身份”的规定，便理直气壮地向敌人提出抗议:我不是军人，你们无权审判，气得敌人大发雷霆，说要枪毙他，但最后还是依法办事，裁定军法司不受理非军人，转送江苏高等法院。难友们都说这是一个很大的胜利。因为转到司法机关以后，可以请律师，官司好打一些。果然，在江苏高等法院，尽管检察官对他的起诉很重:勾结叛徒，煽惑他人，扰乱监狱治安，按规定可判死刑，但最后宣判的结果却是:根据“未遂论”条款，无罪！<br />
<br />
熟悉历史的读者，还会发现，不少革命者和作家可以在“白色恐怖”中成立自己的社团，口诛笔伐，向那一个被他们认为“竟无丝毫自由”的时代发起猛烈攻击而平安无事。<br />
<br />
鲁迅的例子，就不说了，笔者另举一例:看看潘汉年是怎么骂国民党的。<br />
<br />
潘汉年，1925年加入中国共产党，大革命时期任国民革命军总政治部机关报《革命军日报》总编辑和总政治部宣传科科长。“四一二”之后，当年创造社的“小伙计”们都为他担心，在秋风起矣的日子里望眼欲穿，不知他行于何处？这年9月，没赶上南昌起义的潘汉年，一回到上海，就在《幻洲》杂志上宣布:我回来了！<br />
<br />
以下两段文字，摘自于《三十年代在上海的“左联”作家》:<br />
<br />
这时的潘汉年，目睹国民党的血腥大屠杀，拾起杂文这个他所擅长的武器，重新上阵，《原来如是清党》、《血淋淋的头》、《割奶，塞阴户》等篇章强烈控诉了国民党残杀共产党人和广大群众的清党暴行，《共产余毒与吴稚晖》、《吴稚晖先生奈何一言不发》等文章痛斥吴稚晖的反共谬论，而《革命必可成功与鸦片烟老爷》一文更是把批判锋芒直指蒋介石，还在《滑头一下》、《悲夫，本刊放屁之末日将至！》等杂感中对国民党以禁止“赤化”为名、压制出版言论自由作了猛烈抨击，篇篇都如匕首投枪，直刺国民党新军阀的要害。<br />
<br />
战斗的《幻洲》在青年群众中不迳而走，自然引起国民党当局的惧怕和忌恨，一九二八年一月《幻洲》第二卷第八期出版后终于被查禁。但是，潘汉年并没有屈服，不到三个月，他独立主编的综合性周刊《战线》又与读者见面了。他在《战线的开场》□□告:“他们自有他们的权威，我们自有我们的热血，你看吧，我们的战场又在这里开场了……我们甘愿牺牲在权威者的枪弹之下，不能为了个人的苟安偷生而当忠顺的奴才！”<br />
<br />
潘汉年的这些文章，都是公开发表的；<br />
<br />
他当时的住址，也是公开的:霞飞路(今淮海中路)-幢临街的皮鞋店楼上，这里既是宿舍，又是《幻洲》编辑部。由于地处闹市，人来车往，潘汉年为此还将他们的书斋起了一个“听车楼”的雅号。<br />
<br />
即使国民党不知道潘汉年的行踪，但毕竟知道《幻洲》杂志的出版地点。1929年7月11日，在国民党中央执行委员会列出的“反动刊物”一览表中，清清楚楚地标明为:光华书局。<br />
<br />
那么，我们在佩服潘汉年面对白色恐怖坚忍不拔的同时，是不是要感叹一下国民党反动派的屠刀只是把刊物给“咔嚓”了，不仅没把他怎么样，还允许他继续依托光华书局办刊物呢？<br />
<br />
像潘汉年这样的，何止一两个？</div>

							
							
							
							
