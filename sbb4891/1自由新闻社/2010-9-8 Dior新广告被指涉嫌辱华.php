<br />
白人女模穿着时尚优雅霓裳，置身在衣饰、相貌一模一样的中国「复制」人当中，是法国时装名牌 Christian Dior为上海新旗舰店推出的「上海旧梦」系列（ Shanghai Dreamers）平面广告。创意无限，却掀起种族歧视争议，被轰「中国人都像毛泽东思想机械人？多谢了， Dior」。<br />
