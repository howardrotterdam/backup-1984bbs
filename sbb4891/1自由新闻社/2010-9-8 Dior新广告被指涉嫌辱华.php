名牌 Dior这辑广告硬照由中国当红摄影师时晓凡操刀，这已不是两者初次合作。 2008年， Dior庆祝 60周年，在北京举办了一场名为「迪奥与中国艺术家」（ Christian Dior and Chinese Artists）的艺术展，时晓凡为穿着 Dior服饰、由名模杜鹃演绎的一辑「玻璃箱里的陌生人」照片崭露头角，杜鹃处身玻璃箱，在中国旧工厂、旧车站出现，吸引着旧中国人群的目光。<br />
