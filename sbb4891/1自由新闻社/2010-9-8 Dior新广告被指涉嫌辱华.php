<br />
但这辑硬照没引起激赞，反而引起种族主义的批判。硬照映入不少观众眼帘的讯息是：脸孔重复的中国人是没有个性的大众， Dior和西方就代表了个性。<br />
<br />
面对批评，时晓凡澄清：「我希望展现中国人站在一起时的力量，以及中国历史中的社会主义。那些中国模特儿不但是人，而是 1960至 1980年代中国历史的象征。」<br />
