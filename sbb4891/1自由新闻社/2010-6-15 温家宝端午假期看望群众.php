													<h2>温家宝：政府对待年轻农民工 应像对待自己孩子一样</h2>
						
						
																											<div id="postmessage_684730" class="t_msgfont"><img src="http://res.news.ifeng.com/attachments/2010/06/14/25fc84232cf9bb18279af99494edb090.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
<br />
新华网北京6月14日电（记者 李斌 张宗堂）在中华民族传统佳节端午节来临之际，中共中央政治局常委、国务院总理温家宝14日先后来到北京市儿童福利院、西城区富国里社区、地铁6号线平安里站施工工地，看望孤残儿童和城市低保户，了解农贸市场蔬菜供应和价格情况，召开新一代农民工座谈会。<br />
<br />
十分喜爱孩子们的温家宝总理，一直关心和惦记着少年儿童的健康成长。2003年以来，他每年都要抽出时间专门看望少年儿童。<br />
<br />
位于北京海淀区清河镇的北京市儿童福利院，收养了558名孤残儿童，院内绿草茵茵，环境优美。除了照顾这些孩子们的日常生活外，福利院还通过预防保健、引导式教育、生活技能培养、康复训练、心理疏导等，帮助他们健康成长。<br />
<br />
上午8时25分许，温家宝在中共中央政治局委员、北京市委书记刘淇陪同下来到福利院，看望这里的孩子们。福利院主楼大厅正中墙壁的浮雕上，一只大鸟带领着一群小鸟在飞翔。福利院负责人介绍，大鸟呵护着小鸟，寓意着福利院是一个温馨安全的家，象征着这里的保育员和教师们像妈妈一样呵护着孩子们。<br />
<br />
在引导式教育室里，3位教师正在教孩子们包粽子。温家宝走进教室，坐在小板凳上向教师询问孩子们的情况。教师姜红霞告诉总理，这些孩子都是脑瘫患儿，这节课就是教他们怎么包粽子，然后让他们把包好的粽子画下来，目的是培养孩子的观察能力和动手能力。6岁的鲁伯任小朋友尽管行动不便，但还是一副认真学习的模样。温家宝十分疼爱地对他说：“你能站起来给爷爷看看吗？”在大家的鼓励下，小伯任扶着桌子缓缓站了起来，现场响起一片掌声。温家宝说：“我相信，将来你不用扶任何东西就可以走路。”在旁边另一间教室里，正在进行婴幼儿发展训练。温家宝用充满慈爱的目光专注地看着孩子们在康复垫上练习爬行。他对教师们说，爱之所在，家之所在。你们的工作体现了社会对这些特殊儿童的关爱。我感谢你们。<br />
<br />
为迎接端午节到来，福利院儿童小剧场装饰一新。一位教师正在给40多个孩子讲端午节的来历。看到总理走进来，孩子们大声喊“温爷爷好”。总理亲切地招呼着孩子们，坐在他们中间。在《我是小鼓手》的欢快乐曲声中，5个孩子斜挎着小鼓，跳起舞蹈。看着孩子们的表演，温家宝微笑着鼓掌相和。随后，他走到剧场中间，深情地对孩子们说：“端午节快到了，我心里放不下你们，特意来看看你们。你们生活在一个特殊的大家庭里，受到老师和社会无微不至的关爱。你们就像小鸟一样，健康快乐地成长。你们还很小，今后的路还很长。祝你们健康成长，快乐成长。”他还对在场的干部说：“全社会要形成关爱孤老、孤残、孤儿的风气，让人人生活充满阳光。”在孩子们的欢呼声中，温家宝满怀喜悦地把一件件玩具送到孩子们手中。<br />
<br />
随后，温家宝来到北京市西城区富国里菜市场，了解蔬菜供应和价格情况。今年以来，我国一些地方菜价波动明显，温家宝对此十分关心，多次要求地方和部门搞好供应，保障群众基本生活。富国里菜市场面积约2000平方米，为周边的众多小区供应蔬菜。适逢端午小长假第一天，菜市场里人来人往，熙熙攘攘。一个挨着一个的摊位上，蔬菜瓜果琳琅满目。温家宝沿着150多米长的通道，边走边看，边看边问。他不时拿起价签认真查看并询问价格、进货渠道等情况。居民们纷纷围拢过来，向总理问好。温家宝问正在买菜的赵大妈：“我来看看菜价，菜贵吗？”“还行。”他接着说：“菜价和季节有关系。现在是蔬菜旺季，价格下来了。”“对，我们还能承受。”当听到居民们反映这里的瓜果蔬菜新鲜、质量有保证、价格合理时，温家宝的脸上露出了欣慰的笑容。他说，蔬菜的质量和价格关乎每个家庭的生活，关键是要处理好生产、流通和消费三个方面的关系，发挥好政府调控和市场机制的作用。<br />
<br />
接着，温家宝步行穿过狭窄的胡同来到低保户谈志华家。67岁的谈志华住在10平方米的平房里。近些年，在政府的支持下，家里的水、电、热、气都进行了改造。温家宝在床沿上坐下，和老人亲切地唠起家常：“一个月电费多少，冬天取暖生炉子吗？”老人告诉总理，每个月有440元的低保金、200元的无保障老人生活补助、500元的廉租房补贴，每月共有1100多元钱，又住在改造后的房子里，很知足。温家宝说：“你感到满意就好。政府还要想办法，逐步改善群众的生活。”听说老人的孙女在首都师范大学上学，将来要当老师，温家宝鼓励她说“当老师光荣”。<br />
<br />
温家宝十分关心农民工。10时许，总理来到北京地铁6号线平安里站施工工地，看望慰问节日期间仍坚守岗位的工人们，并在工地会议室召开新一代农民工代表座谈会。在北京建筑、制造、安保、餐饮服务等行业工作的50多名年轻农民工代表与总理进行了交流。<br />
<br />
温家宝说：“端午节快到了。我今天特意来看望大家，借这个机会，也向全国广大农民工特别是新一代农民工表示问候和敬意。”<br />
<br />
从湖北进京务工8年的张磊向总理反映，虽然工作比较稳定，但是要想长期在北京安家立业，仍然面临住房难和孩子上学难等问题。温家宝说，成家立业、买房子、生孩子，这是我们年轻农民工遇到的新问题。各地要从实际出发，逐步使有条件的农民工融入城市。<br />
<br />
温家宝说，我们一方面要推进城镇化、工业化，同时要把农村建设成美好家园。把农村建设好了，有好的学校、医院，农民即使出来打工也没有后顾之忧。我们要朝这个方向努力。<br />
<br />
“总理，我要发言。”坐在后排的巫常炜站起来，举手发言。这位28岁的中铁十四局工人告诉总理，虽然公司住房、饮食等生活条件都不错，但是工作强度大，生活单调，因此很羡慕城里人衣着干净，生活悠闲。<br />
<br />
“我很理解你。你提出了一个如何使新一代农民工生活更充实更丰富的大问题。”温家宝说：“28岁的年龄，除了工作自然要想到生活。一是企业要丰富工人的业余生活，比如举办球类、歌咏、体育等活动，二是个人要有远大志向，充分利用年轻的时候多读点书，学点技术。人得有一门爱好，有所追求，这样就会感到时间不够，生活就不枯燥了。”听说巫常炜昨天刚过28岁生日，温总理祝他生日快乐。<br />
<br />
一个多小时的座谈会上，先后有10位新一代农民工发言。大家发言过程中，温家宝不时插话，询问有关情况，会议室里不时传出笑声，洋溢着融洽的气氛。<br />
<br />
温家宝诚挚地说，农民工是当代中国产业工人的主力军，我们的社会财富、高楼大厦都凝聚着你们的辛勤劳动和汗水，你们的劳动是光荣的，应该得到全社会的尊重。要关心农民工、爱护农民工、尊重农民工，尤其是年轻一代的农民工。通过座谈，使我感到，你们富有朝气、热爱学习、积极向上，你们都掌握了一定的技能，工作也很努力，不少已成为单位骨干。政府以及社会各界都应该像对待自己的孩子一样对待年轻农民工。<br />
