流量第一，反响巨大，影响力绝对超过以“良心”自居的《南方周末》。据说，已经被中央高层和外部势力盯上，所以“网评”和“网特”在上面激烈对骂。但自从去年314后，有向中间派转换的倾向。据说，斑竹在拼命封号，这就点中网特的死穴。因为网特就是靠一个人用几十个马甲来搞“人民战争”。<br />
