&nbsp; &nbsp; 　　警方初查，民和县东垣乳品厂于近期分别从河北等地购进奶粉原材料58吨，其中从河北购进原材料38吨，从中检测出三聚氰胺超标500余倍。<br />
&nbsp; &nbsp; 　　记者了解，东垣乳品厂法人代表、厂长刘战峰，男，54岁，陕西省西安市临潼区骊山办乳品厂人；生产厂长王海峰，男，37岁，陕西省西安市临潼区相桥镇柴寨村人。目前，二人已分别被警方依法控制。<br />
&nbsp; &nbsp; 　　经核实，现已查封东垣乳品厂奶粉原料64吨、成品12吨，经青海省质监部门对上述原料4批次样本检验，均检出三聚氰胺含量超标。<br />
&nbsp; &nbsp; 　　记者7月7日前往青海省质监局采访东垣乳品厂三聚氰胺事件情况，质监局办公室主任联系政策法规处一名刘姓工作人员与记者见面。这位工作人员表示，青海省质监局没有向记者提供信息的权力，指出记者应该向青海省卫生厅食品安全协调委员会了解相关情况，并提醒记者学习法律。<br />
