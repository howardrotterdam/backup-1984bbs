&nbsp; &nbsp; 　　为了查清问题奶粉的来源与走向，记者分赴甘肃、青海、吉林等省进行了追踪。<br />
&nbsp; &nbsp;&nbsp;&nbsp;　　记者7月4日从甘肃省质量技术监督局了解到，他们在三份接受委托人送检的奶粉样品中，检验出三聚氰胺超出限量值标准。甘肃质检部门立即通知相关省份进行调查。 (博讯 boxun.com)<br />
<br />
&nbsp; &nbsp; 　　据甘肃省质监局介绍，6月25日，甘肃省质监局产品质量监督检验中心接受委托人刘西平送检的三份奶粉样品，要求检测三聚氰胺一项指标。这三份奶粉样品均用无色自封塑料袋包装，每份100克，上面加贴白色小纸片，用圆珠笔分别写着“样品1”“样品2”等字样。<br />
&nbsp; &nbsp; 　　甘肃质检部门检验发现，三份样品三聚氰胺含量分别为：215mg／kg、1397mg／kg、323mg／kg，分别超出限量值标准86%、559%、130%。<br />
&nbsp; &nbsp; 　　甘肃省质监局副局长王忠习介绍，这三份包装异常、三聚氰胺严重超标的奶粉样品引起了甘肃质监局的怀疑，随即利用当事人取质检报告的机会，对刘西平进行了调查，并移交兰州市公安局经侦支队，同时将情况上报甘肃省政府和国家有关部门。<br />
&nbsp; &nbsp; 　　刘西平交代，他是青海省民和回族土族自治县东垣乳制品厂业务员，问题奶粉就存放在这个厂附近的村子里，供货商为一姓周的陕西人。<br />
&nbsp; &nbsp; 　　甘肃省质监局随即向陕西省、青海省质监局发函，请求协助调查。<br />
