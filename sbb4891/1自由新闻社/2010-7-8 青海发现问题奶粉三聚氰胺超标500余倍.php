													<h2>青海发现问题奶粉三聚氰胺超标500余倍</h2>
						
						
																											<div id="postmessage_728994" class="t_msgfont">导读]甘肃青海吉林再现三聚氰胺超标奶粉。警方在青海一家乳制品厂，检测出三聚氰胺超标500余倍，原料来自河北等地，生产的奶粉主要销往江浙一带。吉林也查出黑龙江生产的三聚氰胺含量严重超标奶粉。<br />
<br />
<br />
河北“三鹿奶粉”阴云未散，超标三聚氰胺奶粉再次“现身”！<br />
<br />
新华网北京7月8日电 (记者 周立权、朱国亮、王大千)河北“三鹿奶粉”阴云未散，超标三聚氰胺奶粉再次“现身”！警方查明，在青海省一家乳制品厂，检测出三聚氰胺超标达500余倍，而原料来自河北等地。<br />
<br />
为了查清问题奶粉的来源与走向，记者分赴甘肃、青海、吉林等省进行了追踪。<br />
<br />
甘肃：检出问题奶粉产自青海<br />
<br />
记者7月4日从甘肃省质量技术监督局了解到，他们在三份接受委托人送检的奶粉样品中，检验出三聚氰胺超出限量值标准。甘肃质检部门立即通知相关省份进行调查。<br />
<br />
据甘肃省质监局介绍，6月25日，甘肃省质监局产品质量监督检验中心接受委托人刘西平送检的三份奶粉样品，要求检测三聚氰胺一项指标。这三份奶粉样品均用无色自封塑料袋包装，每份100克，上面加贴白色小纸片，用圆珠笔分别写着“样品1”“样品2”等字样。<br />
<br />
甘肃质检部门检验发现，三份样品三聚氰胺含量分别为：215mg／kg、1397mg／kg、323mg／kg，分别超出限量值标准86%、559%、130%。<br />
<br />
甘肃省质监局副局长王忠习介绍，这三份包装异常、三聚氰胺严重超标的奶粉样品引起了甘肃质监局的怀疑，随即利用当事人取质检报告的机会，对刘西平进行了调查，并移交兰州市公安局经侦支队，同时将情况上报甘肃省政府和国家有关部门。<br />
<br />
刘西平交代，他是青海省民和回族土族自治县东垣乳制品厂业务员，问题奶粉就存放在这个厂附近的村子里，供货商为一姓周的陕西人。<br />
<br />
甘肃省质监局随即向陕西省、青海省质监局发函，请求协助调查。<br />
<br />
王忠习介绍，目前，青海省质监部门已查获这批问题奶粉，约38吨，青海质监局也检出这批奶粉三聚氰胺严重超标。嫌疑人刘西平于7月3日被移交青海方面。<br />
<br />
从目前掌握的情况分析，很有可能是一些犯罪分子利用过去尚未完全销毁的“三鹿问题奶粉”进行加工、销售，不在本省而在外省检测三聚氰胺含量，目的是为了根据三聚氰胺的含量进行调兑。<br />
<br />
甘肃省质检部门要求本省各地加大对奶粉的抽查力度，严防问题奶粉进入市场。据刘西平交代，他所在的东垣乳制品厂生产东垣牌奶粉，主要销往江浙一带，少量在青海本地销售。<br />
<br />
青海：记者追踪问题奶粉遭遇困难<br />
<br />
记者从青海省一份内部通报上了解到：7月3日，海东地区公安机关根据甘肃省提供的线索，协助质监部门对民和回族土族自治县东垣乳品厂问题奶粉进行了查封。<br />
<br />
警方初查，民和县东垣乳品厂于近期分别从河北等地购进奶粉原材料58吨，其中从河北购进原材料38吨，从中检测出三聚氰胺超标500余倍。<br />
<br />
记者了解，东垣乳品厂法人代表、厂长刘战峰，男，54岁，陕西省西安市临潼区骊山办乳品厂人；生产厂长王海峰，男，37岁，陕西省西安市临潼区相桥镇柴寨村人。目前，二人已分别被警方依法控制。<br />
<br />
经核实，现已查封东垣乳品厂奶粉原料64吨、成品12吨，经青海省质监部门对上述原料4批次样本检验，均检出三聚氰胺含量超标。<br />
<br />
记者7月7日前往青海省质监局采访东垣乳品厂三聚氰胺事件情况，质监局办公室主任联系政策法规处一名刘姓工作人员与记者见面。这位工作人员表示，青海省质监局没有向记者提供信息的权力，指出记者应该向青海省卫生厅食品安全协调委员会了解相关情况，并提醒记者学习法律。<br />
<br />
7月8日10时许，记者又前往青海省卫生厅采访，卫生厅食品安全监管处和应急办无人在办公室。记者向隔壁财务办公室工作人员询问，对方回答“人不在，开会去了。”又询问主管食品安全的厅长怎么联系，对方回答“不太清楚。”随后记者拨打了卫生厅副厅长颉学辉电话，他以早上和下午都要开会、没有时间为由拒绝了记者采访。<br />
<br />
接着，记者在10时48分拨打了青海省公安厅副厅长任三动的办公电话，他说“我们只做不说，有关情况请与青海省政府信息处联系”就挂断了电话。记者拨打民和县公安局常务副局长孙瑞平电话，对方也以“开会”为由挂断了电话。<br />
<br />
11时左右，记者拨打青海省政府信息处电话，接电话的工作人员对于东垣乳品厂三聚氰胺事件表示“不知道”，并让记者联系雷主任，而雷主任的电话打过去也无人接听。<br />
<br />
吉林：紧急封存检测超标问题奶粉<br />
<br />
在甘肃和青海发现问题奶粉前几天，吉林省也查出三聚氰胺含量严重超标奶粉，为此吉林省采取了紧急行动。<br />
<br />
记者7月8日从吉林省工商等部门了解到，吉林市6月22日发现的“三聚氰胺超标奶粉”的样本正在进一步检测中，结果尚未出炉。<br />
<br />
6月22日，吉林市工商局丰满分局在检查中，检测到辖区内一家市场零售点销售的黑龙江省大庆市一家乳品有限公司生产的一袋奶粉三聚氰胺含量严重超标，复检结果与第一次检测结果相同。<br />
<br />
吉林市公安和工商部门迅速展开行动，将1000多袋问题奶粉进行了封存。随后，吉林省对这家企业生产的奶粉进行清查和追缴，并对封存产品进行了抽检。<br />
<br />
目前，吉林省各有关部门对吉林市“三聚氰胺超标奶粉”事件正在展开全面调查，已将这家企业生产的所有产品在全省勒令下架封存。待事件调查清楚后，将由食品安全委员会统一发布相关情况。此外，吉林省质量技术监督局也于8日要求，从9日开始，在全省范围内开展对乳制品生产企业的检查。<br />
<br />
青海、甘肃、吉林等省发现三聚氰胺超标奶粉后，有关部门要求严肃查处，杜绝问题奶粉流入市场，彻底查清其来源与销路，坚决予以销毁，并依法追究当事人责任。<br />
