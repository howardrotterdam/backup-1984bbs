&nbsp; &nbsp; 　　从目前掌握的情况分析，很有可能是一些犯罪分子利用过去尚未完全销毁的“三鹿问题奶粉”进行加工、销售，不在本省而在外省检测三聚氰胺含量，目的是为了根据三聚氰胺的含量进行调兑。<br />
&nbsp; &nbsp; 　　甘肃省质检部门要求本省各地加大对奶粉的抽查力度，严防问题奶粉进入市场。据刘西平交代，他所在的东垣乳制品厂生产东垣牌奶粉，主要销往江浙一带，少量在青海本地销售。<br />
