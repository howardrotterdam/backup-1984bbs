													<h2>北京“天上人间”等4家夜总会被停业整顿半年</h2>
						
						
																											<div id="postmessage_612696" class="t_msgfont">本报讯 (记者于杰)前晚，朝阳警方兵分四路突查天上人间、名门夜宴、花都、凯富国际等4家豪华夜总会，当场查获有偿陪侍小姐557人。4家夜总会被勒令停业整顿6个月。<br />
<br />
　　四路民警夜晚同时出击<br />
<br />
　　11日晚10点多，多辆警车疾驰至三里屯附近的名门夜宴夜总会门前。数十名民警下车后冲进名门夜宴俱乐部，出示证件后对各包房逐一检查。正在包房内陪唱、陪酒的小姐均被民警带到夜总会大堂集中接受调查，一时间大堂内挤满了穿着暴露的年轻女子。<br />
<br />
　　同一时间，另外三路民警会同工商、消防等部门进入东三环附近的天上人间、花都、凯富国际等3家豪华夜总会，突查有偿陪侍等违法活动。当天警方在4家夜总会查获有偿陪侍小姐557人，其中仅名门夜宴俱乐部就查获有偿陪侍小姐199人。<br />
<br />
　　因有偿陪侍被停业半年<br />
<br />
　　朝阳警方透露，天上人间等4家夜总会因存在有偿陪侍，在消防安全上也存在隐患，按照相关处罚规定均被勒令停业整顿6个月。朝阳公安分局一位警官介绍说：“这是有偿陪侍处罚的最高限。”当晚，有偿陪侍小姐被警方遣散，在这4家场所消费的客人也被要求离开。<br />
<br />
　　打击涉黄保持凌厉攻势<br />
<br />
　　自4月11日市公安局开展打击卖淫嫖娼专项行动以来，朝阳警方先后对辖区400余家歌厅、洗浴和无照发廊、足疗按摩场所开展大规模清理整治，集中打掉一批卖淫嫖娼团伙，全区接报卖淫嫖娼警情同比下降三成。朝阳警方称将始终保持凌厉打击攻势，坚决遏制社会丑陋现象。<br />
<br />
　　警方表示，将对存在涉黄问题场所有一家整顿一家，对涉及卖淫嫖娼行为有一起查处一起，绝不姑息。 <br />
<br />
　　&gt;&gt;现场<br />
<br />
　　警察到场，小姐很平静<br />
<br />
　　前晚在花都夜总会，民警冲进一间包房时，几名年轻女子正在陪侍几名男子饮酒、唱歌。对于民警的到来，她们神色平静，在面对警察的询问时，并不隐瞒自己的陪侍身份。但是当民警用相机拍照取证时，几名陪侍女子突然不约而同地扭头、捂脸，男顾客们也举手遮挡脸部。<br />
<br />
　　民警将陪侍女子带到大堂，数十名陪侍女子坐下后纷纷窃窃私语，有陪侍女子不时抬头看一眼现场民警。民警调查取证后，将所有陪侍女子遣散。<br />
