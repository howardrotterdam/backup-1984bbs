过去的两年，在极为困难、复杂的情况下，中国在世界率先实现经济回升向好，保持经济平稳较快发展，这是我们全面实施一揽子计划的结果。在国际金融危机冲击最严重的时候，我国经济增速一度大幅下滑，不少企业处于停产半停产状态，有的甚至倒闭，不少工人失业，大批农民工返乡。由于我们及时实施了一揽子计划，从2009年第二季度开始，经济增速下滑趋势迅速扭转，当年增长9．1％，今年上半年增长11．1％。城镇就业不断增加，居民收入持续增长，社会保持和谐稳定。事非经过不知难。一个13亿人口的国家，如果没有一定的经济增长速度，实现充分就业、提高人民福祉就是一句空话。实施一揽子计划的成效，不仅在于保持了当前经济增长和社会稳定，更重要的是保持了中国经济发展的好势头，现代化进程没有因巨大的外部冲击而出现大的波折，其意义重大而深远。<br />
<br />
在应对国际金融危机中，我们始终高度重视推动经济发展方式转变和经济结构调整。两年来，国内需求特别是消费对经济增长的拉动作用持续增强，2009年社会消费品零售总额实际增长16．9％，为1986年以来最高增速，今年以来这一势头得到延续，上半年实际增速与去年同期基本持平。产业结构升级加快，今年前7个月，高技术产业增加值同比增长17．7％，高于规模以上工业增加值增速0．7个百分点。基础设施建设得到加强，2008年8月1日，我国第一条具有完全自主知识产权、世界一流水平的高速铁路 京津城际铁路通车运营，全程运行时间只有30分钟，使两大直辖市形成同城效应；去年12月26日通车运营的武广高速铁路又成为目前世界上一次建成里程最长、速度最高的高速铁路。节能减排和环境保护扎实推进，去年关停了小火电机组2617万千瓦，淘汰落后炼钢产能1691万吨、炼铁产能2113万吨、水泥产能7416万吨，今年9月底前将再淘汰落后炼钢产能825万吨、炼铁产能3000万吨、水泥产能9155万吨，“十一五”前四年单位国内生产总值能耗累计下降15．6％。区域发展协调性增强，2009年中部地区、西部地区规模以上工业增加值增速分别比全国快1．1和4．5个百分点，今年上半年中部地区比全国快3．1个百分点，西部地区与全国持平，中西部地区占全国规模以上工业增加值的比重由2008年同期的38．1％上升到38．8％。更为重要的是，我们从全局和战略的高度，对加快经济发展方式转变和经济结构调整做出了全面部署。所有这些都将对我国经济长期稳定健康发展起到重要推动作用。<br />
<br />
我们实施积极的财政政策和适度宽松的货币政策，刺激力度前所未有，同时又比较好地控制了财政金融风险。这两年，我国财政赤字和国债规模分别控制在国内生产总值的3％以内和20％左右；银行资产质量和抵御风险能力提高，目前资本充足率和不良贷款率分别为11．1％和2．8％，都处于安全范围内。我们清醒地看到财政金融领域的潜在风险，特别是地方政府融资平台的债务风险，这个问题由来已久，但近期风险有所加大。我们已经出台加强地方政府融资平台公司管理的有关办法，正在抓紧落实。面对突如其来的国际金融危机冲击，我们采取超常规的政策措施是必要的，这些政策措施在发挥积极作用的同时，难以避免会带来一些负面影响，关键是要把这些影响控制在可以承受的范围内。从这个角度来说，我们比较好地处理了兴利与除弊的关系，总体上看效果是好的。<br />
<br />
中国实施一揽子计划，不仅保持了本国经济稳定和较快增长，也为世界经济复苏做出了重要贡献。当主要发达国家经济出现负增长之时，中国等发展中大国经济迅速回稳和保持较快增长，极大地增强了世界战胜国际金融危机的信心，为世界经济提供了强劲增长动力。2009年中国实现进口10056亿美元，全年贸易顺差减少了1020亿美元；今年前7个月，中国实现进口7666亿美元，同比大幅增长47．2％，贸易顺差同比减少226亿美元。这表明，中国经济增长为跨国公司提供了重大发展机遇，为主要经济体和周边国家创造了大量需求，成为世界经济复苏的重要引擎。<br />
<br />
总之，无论从短期看、还是从长期看，无论从实体经济领域看、还是从财政金融领域看，我们应对国际金融危机采取的一揽子计划和政策措施都是符合中国实际的，是及时的、有力的、有效的，是造福当代、利于后人、惠及世界的正确选择。当前中国经济运行呈现增长速度较快、结构逐步优化、就业持续增加、价格基本稳定的良好格局。二季度后一些主要经济指标增速出现回落，主要是基数影响和主动调控的结果。我们完全有信心、有条件、有能力继续保持经济平稳较快发展。我们将坚持把处理好保持经济平稳较快发展、调整经济结构和管理通胀预期的关系作为宏观调控的核心，把稳定政策作为宏观调控的主基调，保持政策的连续性、稳定性，增强调控的针对性、灵活性，进一步巩固和发展好的势头。<br />
<br />
女士们，先生们：<br />
<br />
国际金融危机的深层次影响还没有完全消除，世界经济还没有进入稳步增长的良性循环，系统性和结构性风险仍然比较突出。要巩固和扩大应对国际金融危机冲击成果，并统筹考虑当前和长远发展，在继续促进经济复苏的同时，通过经济结构性的改革，为可持续发展创造条件。这是世界各国面临的共同课题。中国经济发展中也存在一些不平衡、不协调、不可持续的问题，主要是：经济结构不合理，科技创新能力不强，资源环境约束强化，城乡区域发展不平衡，经济社会发展不协调。这些问题有的是经济发展现阶段很难避免的，有的是体制改革不到位造成的。我们将把短期调控政策和长期发展政策有机结合起来，把深化改革开放和推动科学发展有机结合起来，切实解决这些深层次、结构性问题。这样，中国经济才能实现更大更持久的发展。当前和今后一个时期，我们将着重在以下几个方面做出努力。<br />
<br />
我们要坚持内外均衡发展，着力构建扩大内需特别是消费需求的长效机制。中国有世界上潜力最大的国内市场，充分挖掘市场潜力，有效释放国内需求，是促进中国经济长期稳定发展的关键所在，也是解决经济运行中突出矛盾的重要途径。我们将加快推进收入分配制度改革，努力提高居民收入在国民收入中的比重和劳动报酬在初次分配中的比重，创造条件让更多群众拥有财产性收入，尽快扭转收入差距扩大趋势，促进居民收入和消费可持续增长。我们将坚持统筹城乡区域协调发展，积极稳妥地推进城镇化，因地制宜地把符合条件的农民工逐步转为城镇居民，继续加快新农村建设，加强农村基础设施建设和改善公共服务，继续实施区域发展总体战略，大力推进西部大开发和东北地区等老工业基地振兴，促进中部崛起，着力培育内需增长新动力，拓展农村和中西部地区的内需增长新空间。同时，我们要坚持面向国际国内两个市场。中国经济是开放型经济，我国既是出口大国，也是进口大国。我们发展对外贸易，并不追求贸易顺差。目前中国对美国、欧洲等有顺差，但对日本、韩国有逆差；加工贸易有顺差，但一般贸易有逆差；出口恢复性增长很快，但进口增长更快。我们不能、也不会关起门来搞发展。我们将在扩大内需的同时，积极稳定和拓展外需，努力实现内外均衡发展。<br />
<br />
我们要坚持创新驱动，着力推动科技进步和产业结构优化升级。这是从根本上解决我国资源环境约束，适应国际需求结构调整和国内消费升级新变化，全面提升国民经济发展质量、效益和国家竞争力，促进经济可持续发展的战略重点。我们将把提高科技创新能力与完善现代产业体系有机统一起来，用先进技术改造传统产业，培育一批有自主知识产权和知名品牌、国际竞争力强的优势企业，建设一批具有国际水平和带动能力的现代产业集群，促进我国由制造大国转变为制造强国。我们将牢牢把握未来科技进步新趋势，加强政策支持和规划引导，积极培育发展战略性新兴产业，加快形成新的支柱性产业，力争实现跨越式发展。加快服务业对内对外开放步伐，营造有利于服务业发展的政策和体制环境，提高服务业在国民经济中的比重。<br />
<br />
我们要坚持节约资源和保护环境，着力提高资源利用效率和应对气候变化能力。节约资源和保护环境是我们的基本国策。我们必须加快构建有利于节约能源资源和保护生态环境的产业结构、生产方式和消费模式，促进人与自然的和谐统一。我们将进一步完善法规和标准，强化目标责任考核，推动循环经济发展，全面推进节能、节水、节地、节材和资源综合利用，加强对各种自然资源的节约和管理，加强综合治理，保护与修复生态。我们将大力培育以低碳排放为特征的工业、建筑和交通体系，增加森林碳汇，加快低碳技术研发、示范和产业化，全面增强应对气候变化能力，在“共同但有区别的责任”原则下积极开展应对气候变化国际合作。<br />
<br />
我们要坚持经济社会协调发展，着力保障改善民生和促进社会公平正义。保障和改善民生，让经济发展成果惠及全体人民，是经济社会发展的根本目的。让人民生活有基本保障、无后顾之忧，是现代政府的重要职责。目前许多国家都面临失业率高企的共同难题。中国也面临着相当严峻的就业形势。我国劳动力人口近8亿，相当于所有发达国家劳动力资源的总和，每年新进入人力资源市场的劳动力远远超过能够提供的就业岗位，劳动力总量供大于求与结构性用工短缺矛盾并存。我们将把促进就业作为经济社会发展的优先目标，实施更加积极的就业政策，积极开发就业岗位，鼓励自主创业，促进充分就业。我们将着力增强政府提供公共服务能力，逐步形成比较完整、覆盖城乡、可持续的基本公共服务体系，提高社会保障、基本医疗卫生等领域的均等化水平。住房问题既是经济问题，更是影响社会稳定的重要民生问题，稳定房价和提供住房保障是各级政府的重要责任。我们要进一步规范市场秩序，完善土地、财税、金融政策，加快建立促进房地产市场健康发展的长效机制，抑制投资、投机性需求，引导市场增加普通商品房供给，加快保障性住房建设，发展公共租赁住房，促进形成合理的住房供给结构，满足多层次的住房需求。<br />
<br />
我们要坚持深化改革，着力增强可持续发展的动力与活力。中国的发展进步得益于改革开放，中国要实现富强民主文明和谐的现代化目标，仍然要靠改革开放。中国的改革已经到了攻坚阶段，必须以更大决心和勇气全面推进各领域改革。我们要通过深化经济体制和政治体制等全面改革，使整个体制更加适应现代经济发展和社会主义民主政治建设的要求，更加有力地推进社会公平正义，更加有利于人的自由和全面发展。<br />
<br />
女士们，先生们：<br />
<br />
中国的发展是开放的发展。中国的开放是长期的、全面的、互利的。一切有利于对外开放的政策，我们都会坚持下去。中国始终致力于为外商投资企业创造开放公平的良好环境。中国高度重视知识产权保护，已将保护知识产权提升为国家战略，中国也愿意与世界各国进行知识产权交流和对话。在这里，我要重申，所有依照中国法律在中国注册的企业都是中国企业，它们制造的产品都是中国制造，它们研发的创新产品也都是中国创造。在中国境内注册的外资企业都享受国民待遇。同时，中国的政府采购，对外商投资企业和中资企业在中国生产的产品一视同仁、平等对待。<br />
<br />
<br />
中国巨大的市场容量、完善的基础设施、完备的产业配套能力和稳定公平的市场环境，正在吸引越来越多的跨国企业到中国投资兴业。目前中国是世界上吸引外资最多的国家之一，全球500强企业中已有470多家在中国落户。截至今年7月底，中国已累计吸收外资1．05万亿美元，连续18年居发展中国家首位。今年1－7月我国吸收外商投资同比增长了20．7％。外商投资企业在中国总体运营情况良好，取得丰厚回报，不少企业成为其母公司全球业务增长亮点和利润中心。这些情况说明，中国政府营造良好投资环境的努力得到了投资者的认可，提振了外商投资信心。我们将继续完善涉外经济法律法规和政策，改善外商投资兴业环境。我们真诚地欢迎各国企业积极参与中国改革开放进程，也希望各类企业严格遵守中国的法律法规，在中国依法经营，共享中国繁荣进步带来的机遇和成果。<br />
<br />
谢谢大家！</div>

							
							
							
							
