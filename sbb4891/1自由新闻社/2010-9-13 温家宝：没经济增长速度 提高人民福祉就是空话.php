2010-09-13 17:37:42　来源: 新华网<br />
中国国务院总理温家宝在开幕式上致辞<br />
<br />
世界经济论坛2010年新领军者年会（夏季达沃斯论坛）13日在天津开幕，国务院总理温家宝出席开幕式并致辞，新华网进行现场直播。温家宝在致辞中说，如果没有一定的经济增长速度，实现充分就业，提高人民福祉就是一句空话。 【进入2010年夏季达沃斯专题】<br />
<br />
温家宝首先说，尊敬的克劳斯 施瓦布主席，尊敬的各位来宾，女士们、先生们，首先我对第四届“夏季达沃斯论坛”的召开表示祝贺，对各位嘉宾的到来表示欢迎。在世界经济缓慢复苏的关键时刻，本次论坛以“推动可持续增长”为主题，探寻未来发展之路具有很强的针对性和重要的现实意义，我祝愿本次论坛圆满成功！<br />
<br />
温家宝指出，过去的两年，在极为困难、复杂的情况下，中国在世界率先实现经济回升向好，保持经济平稳较快发展，这是我们全面实施“一揽子”计划的结果，在国际金融危机冲击最严重的时候，我国经济增速一度大幅下滑，不少企业处于停产、半停产状态，有的甚至倒闭，不少工人失业，大批农民工返乡。由于我们及时实施了“一揽子”计划，2009年第二季度开始经济下滑的速度得到好转，今年上半年增长11.1%，城镇就业不断增加，居民收入持续增长，社会保持和谐稳定。<br />
<br />
温家宝说，“事非经过不知难”，一个13亿人口的国家，如果没有一定的经济增长速度，实现充分就业，提高人民福祉就是一句空话。实施“一揽子”计划的成效，不仅在于保持了当前经济增长和社会稳定，更重要的是保持了中国经济发展的好势头，现代化进程没有因巨大的外部冲击而出现大的波折，其意义重大而深远。</div>

							
							
							
							
