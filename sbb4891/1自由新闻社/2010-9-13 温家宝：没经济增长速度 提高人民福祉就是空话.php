<br />
　　中新网9月13日电 世界经济论坛第四届新领军者年会(2010年夏季达沃斯论坛)13日下午在天津梅江会展中心开幕,国务院总理温家宝出席开幕式并致辞。温家宝表示,中国的发展进步得益于改革开放,中国的改革,已经到了攻坚阶段,必须以更大的决心和勇气全面推进各个领域的改革。<br />
<br />
　　温家宝表示,我们要坚持深化改革,着力增强可持续发展的动力与活力。中国的发展进步得益于改革开放,中国要实现富强、民主、文明、和谐的现代化目标,仍然要靠改革开放。中国的改革,已经到了攻坚阶段,必须以更大的决心和勇气全面推进各个领域的改革。<br />
