<br />
　　温家宝说,“我们要通过深化经济体制和政治体制等全面改革,使整个体制更加适应现代经济发展和社会主义民主政治建设的要求,更加有力地推进社会公平正义,更加有利于人的自由和全面发展。”<br />
<br />
　　温家宝表示,中国的发展是开放的发展,中国的开放是长期的、全面的、互利的,一切有利于对外开放的政策,我们都会坚持下去。中国始终致力于为外商投资企业创造开放、公平的良好环境,中国高度重视知识产权保护,已经将保护知识产权提高为国家战略,中国也愿意与世界各国进行知识产权的交流与对话。<br />
<br />
　　温家宝表示,目前,中国是世界上吸收外资最多的国家之一,全球500强企业中已经有470多家在中国落户,截至今年7月底,中国已累计吸收外资 1.05万亿美元,连续18年居发展中国家首位。今年1-7月,我国吸收外商投资同比增长了20.7%,外商投资企业在中国总体运营情况良好,取得丰厚的回报,不少企业已经成为其母公司全球业务增长的亮点和利润的中心。这些情况说明,中国政府营造良好的投资环境的努力得到了投资者的认可,提振了外商投资的信心。<br />
<br />
