　　　　 <br />
　　　　2008年11月17日，唐骏又接受腾讯科技采访，透露，雅虎收购案透露，他立刻写信给盖茨，表明反对立场。<a href="http://tech.qq.com/a/20081117/000212.htm" target="_blank">http://tech.qq.com/a/20081117/000212.htm</a> <br />
　　　　 <br />
　　　　这就奇怪了。按照唐骏自己的说法，他在雅虎收购案透露后，几乎在同时作了两个截然相反的评论。一个给媒体，一个给盖茨。而且同样斩钉截铁，肺腑之言。 <br />
　　　　 <br />
　　　　难道唐先生这段时间，公务如此繁忙，人格分裂，开始相信一件事情可以又黑又白，既圆且方。或许，唐先生已经修练到红尘看破，认为每一个商业买卖又对又错的地步。只不过，见了老板媒体老友家人，只挑一半说。挑哪一半说，就像张啸歌中唱到，“有什么话要说，我也无法选择“。 <br />
