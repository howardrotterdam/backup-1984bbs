													<h2>方舟子称唐骏学历造假</h2>
						
						
																											<div id="postmessage_721541" class="t_msgfont">2010-07-03山东商报<br />
<br />
　　新华都集团总裁兼CEO、“打工皇帝”唐骏突然身陷“学位造假门”。从7月1日起，有“打假斗士”之称的方舟子在微博上向唐骏“开炮”，称其一直引以为豪的博士学位系伪造。记者王S实习生徐进<br />
<br />
　　热闹大约是从当天21：10开始的，方舟子发出的微博写道：“唐骏的‘加州理工学院博士学位’是假的，是不是也要大家跟着复制如何造假？”<br />
<br />
　　这个疑问源自唐骏写作的畅销书《我的成功可以复制》。“唐骏《我的成功可以复制》说他1994年到微软工作时才第一次使用电子邮件，感到十分新鲜。我作为生物化学系的学生，1992年就已开始使用电子邮件，他作为“加州理工学院计算机科学博士生”居然没有用过电子邮件？……唐骏造假的证据体现在：加州理工的计算机科学校友名单中无此人，也无人姓唐；美国大学博士论文数据库无此人论文。”方舟子在微博上详细解释。<br />
<br />
　　而唐骏给自己的描述是：1990年-1993年在美国加州理工大学计算机专业就读，获得了博士学位。<br />
<br />
　　截至发稿时，唐骏尚没有就此做出公开回应，依然在忙碌，昨日早上9：35，他写短信发微博，“昨天在谈一家网游公司的收购，国内网游团队很多，有个二万在线就可以支撑下去，感觉做网游是相对保险的。”<br />
<br />
　　昨天下午3:40，方舟子又进一步质疑唐骏：“唐骏在《我的成功可以复制》一书称，他在90年代初到美国自办公司有几项发明，靠卖专利赚了钱并引起微软的重视。但是查美国专利局自1976年以来的专利全文数据库，在1999年之前并无叫JunTang的人获得过专利。唐骏去微软时用的名字还是 JunTang，不太可能用别的名字申请专利。”<br />
<br />
　　昨日，本报记者就“学位事件”电话采访唐骏，他告诉记者，“懒得回复他们。正忙着改剧本，剧本快好了，到时候发布会你要来捧场的。”<br />
<br />
　　他说的剧本，是一件自己认为是很伟大的事情，打算拍一部名为《你行我素》的电影，此部电影还有马云、冯仑、俞敏洪、王石、李开复、潘石屹、张朝阳、李彦宏等企业家参与。“我想通过这部电影说一个在外企工作的年轻人，如何从底层奋斗到高层的故事，以此感染更多的年轻人，让他们学会有梦想，对生活有希望。”<br />
<br />
　　他告诉记者，他最近的生活很惬意，“不再像创业时期那样地忙碌，闲暇之余开始学习品味红酒、听音乐或者拍电影……”前两天，他还在微博里研究讨论“《色戒》的那场经典床戏”。<br />
