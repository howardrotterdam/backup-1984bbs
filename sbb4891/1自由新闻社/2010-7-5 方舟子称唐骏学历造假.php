　　　　唐骏的所谓终身名誉总裁，实际上全称是微软中国荣誉总裁，没有终身。唐骏干脆把中国去掉，加上一个终身，听起来就不一样。连陈永正都没有出面，是陈永正一个手下，吃了午饭，突然发了一个Email 写了两句, 里面给了唐骏这么一个封号，就算完事了。唐骏如获至宝，见人就要拿那个Email 出来，证明自己确实是微软封的。后来那个发Email 的也走了。微软那个时候正在重组中国部，也就是我们国内说的整顿，挺乱的，人心惶惶，反正没人查也没人在意这个事情真假。我在微软的老同事还跟我说过，那还是唐骏花钱请客，买通哥们人家才给帮这么个忙。反正谁知道还能在微软中国呆多久，做个顺水人情，江湖上留条路，跟盖茨没啥事。 <br />
　　　　 <br />
　　　　如果有人有心，让唐骏拿出微软当年封他的人事部公文，他肯定拿不出来。美国企业这种正式加封一定要有Letter Head，还要抄报高层，至少是人事主管备案的。就像他肯定拿不出加州理工的博士文凭原本或者复印件，只能说微软如何如何，来忽悠一阵子一样。 <br />
　　　　 <br />
　　　　这里要说一下微软2003年重组中国部。微软上层对开拓中国一直有顾虑。盗版太厉害。后来慢慢取得共识，中国部还是要发展。但是需要加强管理水平，也就是我们讲得充实领导班子。于是中国部才有了升级，从外面招来陈永正，又建立和总不能够接轨的制度，这才慢慢有起色，开始陆续从总部派人。 陈永正一到任，一看就是要挤走唐骏的架势。 <br />
　　　　 <br />
　　　　微软的这次整顿还上了华尔街日报，又一篇大幅报道。算是个海外企业进军中国如何提升管理的经典案例。意思是说微软意识到原来用的中国部的负责人级别低，缺乏经验，能力不足，微软从各地抽调人员过去，把不行的换下来，如何如何。这里，缺经验，没能力说的是谁啊。 <br />
　　　　 <br />
　　　　本来，微软和许多大的跨国企业一样，对中国部的重视一直嘴上说。因为级别定的低，甚至像中国部总裁这样的职位原来一直没有能力强的中国籍员工愿意去。为什么。一个中国部总裁还抵不上微软总部一个中等的部门经理（相当于组长）拿这个头衔去跳槽当然好，但是2002-2003 那段时间，中国市场很多大公司还在开头。又刚刚碰 到互联网泡沫破灭，美国911以后的大萧条。许多人没有微软总部Career的承诺，不愿意冒这个风险。当然这在2005年以后有很大改变。 <br />
　　　　 <br />
　　　　唐俊升中国部总裁以前就是微软中国的技术服务部负责人。实际管售后服务。升总裁没有多久，鲍尔默和唐骏有了一点接触，就发飚了。鲍尔默到总部，发脾气，说，讲中国市场，怎么用了一个不懂销售，没有 Clue 的人，人事部亚洲部怎么搞的，微软没人啦，要求改（编者按，没Clue 就是啥都不懂，一脑袋浆糊的意思）。后来很快就有了微软中国部的内部调整。 <br />
　　　　 <br />
　　　　所谓盖茨挽留，给了头衔，微软内部中国人当中就是个笑话。可能吗？鲍尔默和盖茨什么关系。被鲍尔默几乎全盘否定的一个中下级经理，会让盖茨这样挽留吗。另外，微软的内部程序，给公司一级的什么荣誉头衔，都要人事部报董事会批，因为这样的头衔非同小可。唐骏如果以微软的名义去签字什么，出了事在美国股东可以起诉。那赔起来，都是好几亿好几十亿的风险，因为微软是Deep Pocket，换句话说是大款。老美打官司，大款那才赔得凶。唐骏把微软说成夫妻老婆杂货铺了，一拍脑袋啥都可以。 <br />
　　　　 <br />
　　　　在当时那个情况下，唐骏不要说盖茨，就是离盖茨好几层的亚洲部头头都见不到。 <br />
　　　　 <br />
　　　　不多说了。反正唐骏是充分利用中国人不了解西方企业制度，又非常崇洋的心态，把信他的中国人都山寨了这么多年。但是微软的中国同事又不好多说啥。谁让微软一开始不重视中国部，弄得只有唐骏这样的人去呢？ <br />
