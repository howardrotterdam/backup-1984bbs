　　　　 <br />
　　　　看来，唐骏不仅讲说出来了，而且透露了这样的信息。 <br />
　　　　 <br />
　　　　作为微软中国总裁，他一边命令微软中国封杀方兴东，一边鼓励方兴东到外面开博客网，发表文章，和微软接着斗。 <br />
　　　　 <br />
　　　　企业家，大老板们，要有这样一位朋友，顾问，职业经理人，不知道他们敢不敢相信他？他在赞美你的主张是最好的时候，一定会告诉别人，你的想法是最错最蠢的。你取得成功，他也一定到处宣扬，因为你听了他。反之，他也会到处广播，没有听他，所以你失败。 <br />
