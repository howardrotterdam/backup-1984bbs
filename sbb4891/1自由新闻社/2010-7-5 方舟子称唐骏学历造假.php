&nbsp;&nbsp;2009-09-05 11:28:28: 王尔立　　唐骏的故事在继续 - 2008忽悠露馅记 <br />
　　　　 <br />
　　　　有关当代杰出海龟硕士，美国公民唐骏，2008年中国互联网曾经就两件事，做了大量报道。一是唐骏如何点评，同时赞成而且反对雅虎收购案，一是唐骏如何劝说支持同时阻止方兴东开立博客。让我们来回顾一下。 <br />
