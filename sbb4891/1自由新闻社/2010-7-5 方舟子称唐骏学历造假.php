　　　　 <br />
　　　　“唐骏称，当年中国有两个最爱微软的人，一个是他自己，另一个就是方兴东。但方兴东不断写文章，和微软“过不去”。于是唐骏让微软中国公关总监解决方兴东这个麻烦。 微软中国很快把事情“搞定”，媒体上不再出现方兴东的文章。” <br />
　　　　 <br />
　　　　仅仅4天以后，唐骏先生在演讲中说方兴东开立博客网是因为听了他的劝告，当初方兴东的文章遭微软封杀，来找唐骏，唐骏自称劝说方兴东如下： <br />
　　　　 <br />
　　　　2008年 11月1 日草根网， <br />
　　　　 <br />
　　　　唐骏在近日演讲中回忆当年谈话 <br />
