<br />
　　我来818唐骏。唐骏乃厚黑高手。这个人属于那种自奉清高而道德败坏的货色。话说得很动听，但是全都是假大虚空，压榨又特别狠，演讲时做痛苦万分状，脸都皱起来啦。此人在厚黑学造诣匪浅，尤其是厚字的功力很深，多肉麻，多违心的话都说得出来。口头禅是：你们都是最好的员工，我太爱你们啦。讲完必定要大家合唱“真心英雄”，还要大家热切的围绕在他周围，做和谐状。如果谁不参与这一套恶心的仪式，则会遭到排挤 <br />
　　　　 <br />
　　　　唐骏的绝招说难听点是职务行贿。也就是免费给一些美国的部门干活。美国技术支持部门拿出来一些技术支持的活给中国的技术支持中心，指定了多少名额的人来完成这些事情。唐骏拼命压榨，只用一半的人就做完了。剩下的名额被他用来向Lxx Mxx，Mxx Rxx，以及一些其它的美国人去示好。凡是反对这种做法的，都被他搞掉。后来这个还挺奏效，其实什么微软亚洲技术中心，全球技术中心就是个技术支持中心。为了看上去好看，甚至把美罗大厦的24楼封为总部，其它楼层算是分部，真是滑天下之大稽。主要是用来骗骗刚毕业的大学生，给大家一个貌似有职业发展前途的印象。毕业于西安交大的G某，在大会上被唐骏感动，发言的时候把唐骏说得就像再生父母一样，哭得个稀里哗啦的，太傻冒了。好像放弃了剑桥大学的Offer，当了唐骏的秘书之一。 <br />
　　　　 <br />
　　　　　　后来唐骏终于挣得了足够的政治资本，被Mxx Rxx推荐到了微软中国，没干多久就完蛋了。唐骏拿这个作为吹牛资本很久了，其实也只是一个很低级的大陆地区销售的头的职务，都管不到市场，服务等部门。而且才上任几个月就被Steve Ballmer骂下来，斥之为一点也不懂销售，然后就一直赖着不走，每天也不上班。就会说，我太爱这家公司啦，我一定不会走的，后来要到了Package还不够，才40来岁又要了退休待遇，这个已经是脸皮厚到极点了。可是最令人抓狂的是，他最后居然又要到了一个“微软中国名誉总裁”，我也不知道谁封的，所以说他独得厚字。实际上，这是微软通过中国部的一个部门副总裁发的鼓励奖，级别远远没有他自己说的高。 <br />
　　　　 <br />
　　　　　　至于地震捐款，唐骏拿出来一块劳力士手表拍卖，他竟然说是啤酒盖子送给他的手表。这个手表每年都有好多表现好的员工会拿到，即使最底层的销售或者工程师也能拿到，是Golden Circle Club（算是一种优秀员工吧）的纪念品，他倒真好意思拿出来吹牛。就唐骏最鼎盛时候负责的业务规模，在微软全球可能也占不到0.5%。一直到他都离开盛大了，还整天拿微软说事，说明这几年也真没啥出息。 <br />
　　　　 <br />
　　　　　　上次我看到新闻上有阜阳的县委书记搞了阅兵式，检阅了警察，城管等人。我不由得想起唐骏。中国人的劣根性，在他的身上表现得淋漓尽致。 <br />
　　　　 <br />
　　　　唐骏吹牛的资本还有几个经典案例: <br />
　　　　　　1. 所谓的4大发明，分别是卡拉OK计分机，配对机，大头贴，以及唐氏开发模式 <br />
　　　　　　2. 12岁在冬天冰冷的河水里面搬砖头 <br />
　　　　 <br />
　　　　第一个牛皮现在被证明是假的。如果把唐骏的所有自述的时间加起来，那是牛头不对马嘴。唐骏说该技术被卖给三星。 <br />
　　　　 <br />
　　　　 但是网上搜索专利库，发现卡拉OK计分的专利属于下面两个人，而且都申请了美国专利，国际专利等等。唐骏的专利在哪里？其它的两个实物发明连专利都不知道怎么搜。 <br />
　　　　　　<a href="http://www.patentstorm.us/patents/5719344/description.html" target="_blank">http://www.patentstorm.us/patents/5719344/description.html</a> <br />
　　　　　　<a href="http://www.patentstorm.us/patents/5889224/claims.html" target="_blank">http://www.patentstorm.us/patents/5889224/claims.html</a> <br />
　　　　且慢，唐骏在后来的采访中又说了，当年我不太懂去靠专利赚钱，所以我只是技术的原创者。OK，这下死无对证了。 <br />
　　　　 <br />
　　　　　　至于实现了中英文同步开发的唐氏开发模式，又是一个死无对证的吹牛。其实唐骏从来没有表现出来他也懂技术。 <br />
　　　　 <br />
　　　　唐骏声称他“拿到了加州理工学院的计算机科学博士学位”，但唐骏这完全是造假的，加州理工学院非常小，其计算机科学校友的全部名单(<a href="http://www.cs.caltech.edu/cspeople/alumni.html" target="_blank">http://www.cs.caltech.edu/cspeople/alumni.html</a> )，里面根本就没有唐骏的名字。 <br />
　　　　 <br />
　　　　　第二个牛皮后来发展到8岁就为将来娶媳妇造了一座房子。 <br />
　　　　据说当初唐骏就在日本拿到过一个硕士，博士是他自己吹出来的，包装自己用，唐骏在微软只是个小人物，微软用他主要是用他来管刚毕业的大学生的（上海的技术中心-就是客户服务），用不着什么高学位。一个谎话说十几年，慢慢的总有人信了！ <br />
　　　　 <br />
　　　　记得那时候网上有专访TJ的文章,就说到是他在美国期间开发的一套什么语音软件什么的,对微软贡献很大,不过真没看出来他哪个地方懂技术,另外去盛大后,业界一直说是盛大看重他在华尔街和那些投资家的关系,真真假假,假假真真 <br />
　　　　 <br />
　　　　唐骏就是个垃圾， 规定进他的办公室不能转身出来，要面对他倒着退出来。 <br />
　　　　 <br />
　　　　盛大员工一直不喜欢TJ这个人,感觉就是个三姓家奴,整天就会炒作自己,也没见做出什么业绩,到盛大的时候新闻炒的多厉害,结果现在盛大连个屁都不是 <br />
