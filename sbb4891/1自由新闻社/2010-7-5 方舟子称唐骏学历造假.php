　　从7月1日晚上开始，科普作家方舟子质疑唐骏学历造假的微博客再一次引发了一次微博地震。从7月1日晚上10点16分开始，方舟子开始在本人的微博客上发表有关唐骏学历造假的微博客。方舟子列举了一些唐骏在其自传《我的成功可以复制》里所陈述的学历问题，然后一一展示相关证据，希望证明唐骏的博士学位有问题。昨天，记者致电唐骏本人，和以往他亲自接电话的习惯不同，这一次是唐骏的秘书李小姐接听的电话。在电话里，李小姐对记者表示：“方舟子这个人有问题，是个偏执狂，我们不准备回应，因为回应这样的人的问题，让我们也显得很无聊！”<br />
<br />
　　方舟子微博客<br />
<br />
　　质疑唐骏学历<br />
<br />
　　从7月1日晚上10点16分开始，方舟子对唐骏的炮轰就悄然开始了。方舟子的第一条微博客写道：“唐骏《我的成功可以复制》第二章第56节最后一段： ‘办到第二家公司，我差不多已放弃了学业。但凭借语音识别方面的应用性研究成果，我最后还是拿到了加州理工学院的计算机科学博士学位。’――假的，除非这个‘加州理工学院’是个野鸡大学的巧妙翻译。”<br />
<br />
　　随后，方舟子拿出证据说：“美国名牌大学不是中国名牌大学，不会允许你‘在职读博’，靠糊弄出一个‘成功’或一篇论文就可以获得博士学位，那是卖文凭的野鸡大学的做法。当然，如前所述，唐骏造假的证据体现在：加州理工的计算机科学校友名单中无此人，也无人姓唐；美国大学博士论文数据库无此人论文。”<br />
<br />
　　当天晚11点09分，方舟子继续发力，在微博客上写道：“唐骏《我的成功可以复制》第二章还说他放弃日本的博士学位，到加州理工学院电子工程系做博士后研究。博士后，顾名思义，是获得博士学位之后做的研究。没有博士学位，如何做博士后研究？”<br />
<br />
　　除了学历问题，方舟子昨天15点41分甚至对唐骏自述中的专利发起了冲击，他再发微博客写道：“唐骏在《我的成功可以复制》一书称，他在90年代初到美国自办公司有几项发明，靠卖专利赚了钱并引起微软的重视。但是查美国专利局自1976年以来的专利全文数据库，在1999年之前并无叫Jun Tang的人获得过专利。唐骏去微软时用的名字还是Jun Tang，不太可能用别的名字申请专利。”<br />
<br />
　　唐骏秘书回应：唐骏不会造假<br />
<br />
　　昨天记者致电唐骏本人，希望对唐骏的学历问题求得证实。与以往唐骏本人亲自接电话的习惯不同，这一次是唐骏秘书接听的电话。她对记者表示，“方舟子炮轰唐骏学历这已经不是新闻了，早在半年前就已经有过一轮。我们认为这个人是偏执狂，他写这些完全就是为了自己出名，为了炒作自己，我们不想回应。”<br />
<br />
　　记者随即提问：“如果把唐骏先生的学历证明公布于众，大家的质疑也就烟消云散了，为什么不用这个简单的做法证明自己？”李小姐告诉记者：“如果质疑的人是唐骏的老板，他的上司，那么我们有义务向老板证明，现在方舟子的动机不纯，我们很了解这个人，我们没有义务去理会这个人，再说了，唐骏也不是单靠文凭吃饭的人！”<br />
<br />
　　记者再度追问：“唐骏是否真的拿到了加州理工大学的博士学位？” 李小姐说：“唐骏是个透明的人，他如何求学的经历都写在了《我的成功可以复制》里。他不会舞弊的。”<br />
<br />
　　记者在互联网上搜索发现，凡是唐骏的公开简历里，最高学位都是博士。如同他在《我的成功可以复制》里，他自称是加州理工大学的博士。</div>

							
							
							
							
