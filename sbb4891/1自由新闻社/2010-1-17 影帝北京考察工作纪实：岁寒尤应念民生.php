在北京市朝阳区的保障性住房项目弘善家园，温家宝走进尚未入住的房间仔细察看。新华社记者姚大伟摄 <br />
<br />
　　岁寒尤应念民生――温家宝总理在北京考察工作纪实<br />
<br />
　　正是三九寒冬，北京最低气温已是零下10摄氏度。1月16日，中共中央政治局常委、国务院总理温家宝在中共中央政治局委员、北京市委书记刘淇等陪同下，利用周末时间，一大早就冒着严寒来到农产品批发市场、供热公司、保障性住房小区，调查了解民生情况。<br />
<br />
　　近一个时期，受持续低温降雪天气影响，北方主要城市菜价均有不同程度上涨。每天都看各地价格监测报表的温家宝总理十分关心蔬菜供应和市场菜价情况，今天要实地看看。<br />
<br />
　　温总理到的第一站是新发地农产品批发市场。这里供应了北京地区70%的蔬菜和80%的水果，是北京地区最大的农产品批发市场。上午8时许，温家宝走进蔬菜储备库。高高的仓库里，一袋袋土豆、大蒜、白菜和菜花装得满满当当。总理一连看了5个仓库，还向工作人员询问货源、储备品种等情况。听说储备库里有3200吨蔬菜，全市有1万吨储备菜，能及时满足市场供应，总理脸上露出欣慰的笑容。他叮嘱随行的有关部门负责人说，作为一个大城市，最重要的就是要确保渠道畅通，储备充足。工作人员说：“总理，北京市政府储备蔬菜是从奥运会开始的。”总理表示肯定：“这件事做得对，要坚持下来。政府储备，能起到安定人心的作用。”<br />
<br />
　　随后，温总理迎着寒风，踩着泥泞的道路，来到露天蔬菜交易点。这里人头攒动，十分热闹，一辆辆来自山东、河北、海南、广西等地的大货车旁，人们正在紧张卸货、交易。温家宝微笑着和大家握手，并和商户、货车司机亲切交谈。商户鲁德传刚从海南运来一车蔬菜，正在忙着卸货。总理详细询问蔬菜产地、品种、价格和运输销售情况，并对他说，蔬菜供应主要有三个环节，一是生产，二是流通，三是储备和销售，缺一不可，都是为了满足群众需要。鲁德传接过总理的话茬：“对，就是满足群众的菜篮子。”他的话说得大家都笑了起来。<br />
<br />
　　这时，海南驻北京农产品流通办事处主任黄栋挤进人群告诉总理，海南为北京种植的冬季瓜菜面积比过去又有所增加，温家宝说：“海南蔬菜供应北京，既带动了海南农业的发展，又保证了首都群众的需要。”<br />
<br />
　　“总理，给您一张名片。”黄栋从身上拿出一张名片。总理接过来，微笑着对他说：“如果北京没菜，我就找你。”总理幽默的话，把大家逗得笑了起来。 <br />
　　几辆大货车旁，北京泰和创业精品果业公司总经理赵新文正在过秤记账。温家宝走过去问他：“有什么困难？”<br />
<br />
　　“冬天冷点，但正常。”<br />
<br />
　　“北京上千万人等菜吃，冷一点也值得。”<br />
<br />
　　“民以食为天，我们在做天大的事。”<br />
<br />
　　听了他的话，总理和大家都笑了起来。温总理认真地说：“这话不假，你们的工作直接关系群众生活。”<br />
<br />
　　人们渐渐围了过来，越聚越多。温总理亲切地和大家握手，传递着党和政府对民生问题的关切……<br />
<br />
　　地处天坛公园东门的物美便利超市天东分店主要供应粮油蔬菜及各类副食品。温家宝走进这家超市，只见货架上各类食品琳琅满目。由于是周末，顾客明显比平常多。他沿着货架细细看过去，特别留意商品的价格。还不时停下脚步，与购物的群众亲热地聊天，了解他们的收入和生活状况。离开时，温家宝对大家说，商品供应尤其要抓好流通和储备工作，真正做到生产足、货架满、价格稳、人心安。<br />
<br />
　　天寒地冻，北京市供暖情况牵动着温总理的心。目前，北京市总供热面积6亿多平方米，其中住宅面积占72%。温家宝来到位于丰台区的京桥热电有限责任公司考察。在锅炉车间主控室，温家宝详细了解燃气、供热保障和极端寒冷天气下的应急预案，询问了供热管网建设情况，并看望热力系统职工。在听取北京市有关负责人关于首都天然气供应发展规划时，温家宝说，要保证首都电力、天然气和煤炭等能源供应。坚持先民用后工业的原则，贯彻节能的方针，通过科学管理和合理调度，做到省气、省热、省电，尤其要严格控制高耗能、高污染和产能过剩的产业发展。<br />
<br />
　　地处北京市朝阳区的弘善家园，是为修缮整治前门历史文化保护区、安置外迁居民而建设的保障性住房项目，部分居民已经入住。温家宝来到这里。走近尚未入住的一居室、两居室套房，从卧室、厨房到阳台一一察看，详细询问。看到户型合理，节能保温，他十分高兴。<br />
<br />
　　在64岁的居民宋国赞家，温家宝和老两口促膝而谈。在政府的支持和补贴下，宋国赞一家从前门原来14平方米的平房搬进了现在这套73平方米的两居室，自己花了不到两万元。他激动地对总理说：“这里多宽敞，多明亮，我们非常满意。”温家宝告诉他，国务院刚出台了促进房地产市场平稳健康发展的11条政策，其中最重要的一条就是政府要加快保障性安居工程建设，解决低收入困难家庭的住房问题。<br />
<br />
　　北京是文化古城，旧城区改造任务十分繁重，近年来本着“修缮、改善、疏散”的指导思想加大了改造力度，去年修缮完成近50万平方米，2.3万户居民的住房条件得到改善。温家宝来到前门街道草厂9条3号院，这里原来残破不堪，修缮后，小院焕然一新。温总理先后走进李爱萍和孙春莲两家，坐下来亲切地聊起家常。李爱萍高兴地带着总理参观客厅和厨房，她说，政府出钱对小院进行了修缮，住房条件大大改善，现在有了天然气，既干净，又方便。孙春莲的儿子和儿媳妇送给总理两个大红的中国结，希望总理保重身体。<br />
<br />
　　北京市有关负责人向总理介绍，北京市认真贯彻落实国务院部署，全面推进保障性安居工程，计划三年基本解决28万户困难居民的住房问题，2009年已经完成了三分之一。温家宝赞赏道，这是一个民心工程，要继续抓紧实施，把好事办得更好。<br />
