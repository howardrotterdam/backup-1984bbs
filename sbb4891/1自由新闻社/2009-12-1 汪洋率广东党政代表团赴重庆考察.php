2009-12-04 01:27:00　来源: 南方网　跟贴 203 条 手机看新闻<br />
<br />
<a href="http://news.163.com/09/1204/01/5PLDVKLB000120GR.html" target="_blank">http://news.163.com/09/1204/01/5PLDVKLB000120GR.html</a><br />
<br />
核心提示：中央政治局委员、广东省委书记汪洋将率代表团造访故地重庆，作为原市委书记的汪洋，此次故地重游，就是要加强和重庆的经贸合作。据悉，2009年两会时，薄熙来遇到汪洋，当即就邀请汪洋书记访问重庆。<br />
<br />
南方网12月4日报道 中央政治局委员、广东省委书记汪洋将率代表团造访故地重庆，作为原市委书记的汪洋，此次故地重游，就是要加强和重庆的经贸合作。<br />
<br />
在传达2009年全国“两会”精神电视电话会上，薄熙来在会上透露了这样一个鲜为人知的秘密，薄熙来说：“全国‘两会’的时候，我遇到汪洋书记，我当即邀请汪洋书记来重庆，为了邀请到汪洋书记，我对他（汪洋）说，你要是不来，我回去就跟重庆的同志说，说你乐不思‘蜀’（1997年以前，重庆隶属四川）”。说完，电视电话会场爆出了长时间的欢笑声，薄熙来自己也欢快地笑起来了。<br />
<br />
不得不佩服薄熙来的幽默和睿智。邀请经济总量第一的广东省的“一把手”故地重游，薄熙来的意图很明显，就是要加强重庆和广东的战略合作，使得两地的资源更加互补，合作更加紧密。<br />
<br />
而在经贸合作方面，重庆和广东的互补性特别强。呈现出几个亮点和特色，将两地有机地联系起来，无疑，对广东和重庆双方都是一件幸事。<br />
<br />
重庆作为重要的工业生产基地，渝粤合作的空间巨大，产业转移机遇难得，重庆要成为内陆开放高地，一方面要承接好沿海产业转移带来的机遇，另一方面也要做好高新技术产业发展这篇文章。除此之外，还要想办法配齐好能源、交通、金融、人才、市场及物流这些经济发展的重要要素。<br />
<br />
<br />
汪洋在广东省，着力强调要实现广东省的产业升级，要大力发展现代产业体系，即使在经济危机爆发的时候也不例外。可以说，重庆的经济和广东的互补性强，两者开展经贸合作的潜力巨大。重庆有着传统的人力资源优势，广东也有着产业转移后续发展的优势，两者合作可谓双赢。<br />
<br />
这次的经贸之旅，将签订广东重庆两省市战略合作协议。汪洋作为重庆的原市委书记，对重庆的感情可谓笃厚，在薄熙来执掌重庆将近两年以来，薄熙来亲手勾勒的“五个重庆”战略蓝图渐入佳境，随着金融市长黄奇帆转正，重庆经济板块将强势上扬，资本要素将更加关注重庆，这样以来，这个协议带来的后续效果更加值得期待。<br />
<br />
