网络新思维<br />
<br />
　　新思维<br />
<br />
　　信息和资本、土地一样，是一种需要进行经济核算的生产要素。而互联网的文化核心，是信息的自由流动，是用户的全面参与。这是时代大势所趋。<br />
<br />
　　理财一周报记者/陈才 <br />
<br />
　　综合报道<br />
<br />
　　<br />
<br />
　　没有一种互联网传播手段能像Twitter（一个微型博客网站）一样，在全球引起如此大的争议。<br />
<br />
　　与老一代博客用户渴望表达思想的写作不同，新一代网民更习惯简单、快速的沟通，他们在Facebook上呼朋唤友，在Twitter上发布一句话博客。<br />
<br />
　　在中国国内，Twitter还是一个陌生的词语。当网民还在为邓玉娇案的胜利而欢呼的时候，在伊朗德黑兰，人们已经通过使用Twitter向全世界传递信息。<br />
<br />
　　最近个一月来，从“绿坝”安装，到谷歌“涉黄”，再到Twitter掀起的世界性波澜。一切都暗示着，一个新的互联网时代将被重新演绎。<br />
<br />
　　被Twitter所改变的世界<br />
<br />
　　两年多前，杰克・多尔西（Jack Dorsey）、埃文・威廉姆斯（Evan Williams）和比兹・斯通（Biz Stone）才开始建立Twitter，而时至今日，Twitter已经成为世界上增长速度最快的网站。互联网分析公司Hitwise本周发表报告称，Twitter最近几个月的访问量激增，在过去一年访问量增长了22倍。<br />
<br />
　　Twitter当初只想做个社交网站，如今却被推上“革命之路”。<br />
<br />
　　本月13日，伊朗内政部宣布现任总统马哈茂德・艾哈迈迪・内贾德以压倒性优势赢得连任。主要竞选对手、前总理米尔・侯赛因・穆萨维指责选举存在舞弊，他的支持者连日来在首都德黑兰等地举行抗议活动，数次与警方发生冲突。<br />
<br />
　　在随后的大游行中，Twitter成为当地人们与外界最重要的通讯工具。虽然Twitter对用户发布信息的长度限制在140个字符，但就是从源源不断地上传到网络上的两三句话，令世界更及时地了解到当地所发生的一切。<br />
<br />
　　《时代》周刊分析，Twitter成为“时势之选”的原因很多：它是免费的，具有高度移动性，非常便捷和个人化。它的本职就是传播，而且速度极快；它天生跨界，在互联网和短消息服务网络间自由游走，几乎可以在任何有屏幕和网络连接的设备上收取和阅读。<br />
<br />
　　也正因如此，没有人把Twitter当成一种单纯的社交工具。Twitter不仅是实时新闻的来源，更是重要的通讯媒介。美国国务院甚至要求该公司暂停既定的系统维护，让伊朗民众的通讯不会中断。<br />
<br />
　　这一切令伊朗政府大感恼火。他们军管了互联网，却无法战胜Twitter，只好再次严厉谴责美国政府，称Twitter是一种“新颠覆手段”。<br />
<br />
　　但美国前副国家安全顾问马克・伯斐弗（Mark Pfeifle）在接受采访时称，Twitter创始人应该成为下一届的“诺贝尔和平奖”得主。<br />
<br />
　　事实上，Twitter很快就展现了它的另一面：周四，由于大量粉丝争相通过Twitter了解有关摇滚巨星迈克尔・杰克逊（MichaelJackson）死亡的最新消息，或发表看法，杰克逊死亡成为Twitter上人气最高的话题。<br />
<br />
　　无论如何，Twitter首先是一种社交和通讯工具。<br />
<br />
　　“绿坝”走到关键时刻<br />
<br />
　　而在国内，一款叫做“绿坝”的软件，正成为网民讨论的焦点。<br />
<br />
　　本月8日，工业和信息化部下发《关于计算机预装绿色上网过滤软件的通知》。通知中规定：“计算机生产及销售企业应于2009年6月底完成‘绿坝-花季护航’（以下简称”绿坝“）软件预装测试等相关工作，2009年7月1日后出厂和销售的计算机应预装‘绿坝’软件。”<br />
<br />
　　媒体报道称，该软件可以拦截色情内容、过滤不良网站、控制上网时间等，还可以每3分钟对网页截一次屏，软件使用者打开就可以知道其他使用者登录过哪些网站。<br />
<br />
　　该新闻随后便成为舆论关注的焦点，引起各方强烈反响。<br />
<br />
　　尽管“绿坝”的供应商――郑州金惠公司称，该软件不会侵犯客户个人信息，“不会强制安装”、“可以卸载”，但并没有消除网民的担心。<br />
<br />
　　“绿坝”会不会成“窃听器”？“绿坝”是不是一个圈套？“绿坝”真的能够阻拦网络黄流吗？“绿坝”会不会过滤掉网民的选择权？……网民纷纷发出质问。某门户网站调查显示，八成网友会拒装“绿坝”。<br />
<br />
　　6月10日，《中国青年报》发表署名文章称，预装过滤软件是一种不民主的“家长制”善意。文章援引美国公民权利联盟的话：民众能看什么东西，不应该由政府说了算，那些属于个人决定，应该由个人和他们的家庭来决定。<br />
<br />
　　著名IT人士、互联网实验室总裁方兴东在博客上撰文指出：政府直接出面强制安装过滤软件，“好心不一定办好事”。<br />
<br />
　　美国密歇根大学（University of Michigan）随后发布“绿坝”测试报告指出，该软件存在盗版、侵权、编制粗糙、漏洞多、误判率高等问题。<br />
<br />
　　著名80后作家韩寒则以《绿坝系统提醒你，以下内容包含不良信息》为题作文，对此大加调侃。<br />
<br />
　　距7月1日“大限”越来越近，“绿坝”事件的结局愈受关注，但仍待观察。<br />
<br />
　　谷歌“涉黄”再掀波澜<br />
<br />
　　“绿坝”事件争议未止，谷歌中国（google.cn）涉黄事件，再次掀起波澜。<br />
<br />
　　6月18日，中国互联网违法和不良信息举报中心在首页显著位置上刊登消息，“强烈谴责谷歌传播淫秽色情和低俗信息”。该中心在消息中指出，“谷歌中国”网站大量传播淫秽色情和低俗信息，严重违反国家有关法律法规，违背社会公德，损害公众利益。<br />
<br />
　　当天晚间，中央电视台《新闻联播》和《焦点访谈》节目将此事作为重点新闻向全国播报，随后各大网络、纸媒纷纷跟进，再一起成为热点新闻。<br />
<br />
　　随后，谷歌不得不紧急向公众道歉，并发表声明，表示正在对所提供的所有互联网搜索服务进行彻底检查，努力摈除网络低俗信息。<br />
<br />
　　今年年初，国家新闻办、工信部以及公安部等7部委曾联合开展整治互联网低俗之风的专项行动。今年1月和4月，中国互联网违法和不良信息举报中心分别对谷歌进行了曝光，但稍稍好转一点之后“很快就又恢复（涉黄）了”。<br />
<br />
　　一些人士也猜测，谷歌涉黄与此前“绿坝”的推出有关。资深媒体人士安替指出，二者“是有逻辑联系的”。<br />
<br />
　　安替在接受理财一周报记者采访时表示，谷歌的搜索结果是计算出来的，出现不雅的提示信息，完全是网友搜索的结果，不是谷歌的责任。“谷歌在这方面是完全保持技术中立的，要怪只能怪中国网友对色情信息的渴求。”<br />
<br />
　　因恶搞视频《一个馒头引发的血案》走红的胡戈在博客中表示：批谷歌的意图在于“恐吓”所有的网站经营者。胡戈认为，主管部门的愿望就是：“我不需要监督你，我也不需要通知你删除什么，你自己就会主动对内容进行审查，主动揣摩我想要删除什么，主动删除相关信息。”<br />
<br />
　　在随后几天中，有网民反映，谷歌中国多次出现不能正常登录的现象。<br />
<br />
　　新互联网时代之演绎<br />
<br />
　　2009年，互联网令人有些“心神不宁”。<br />
<br />
　　6月18日，《焦点访谈》播出谴责谷歌中国链接色情信息的节目，有个叫高也的大学生（网上很快即传出此大学生是《焦点访谈》的实习生）谈到该事时说，黄色信息害得他的同学“心神不宁”。但经网民调查，该受访者是《焦点访谈》栏目的实习生。<br />
<br />
　　有专家认为，当网民超过人口的10%时，互联网就将成为主流媒体。截至2008年底，我国网民总数已经达到2.98亿，较2007年增长41.9%；我国互联网普及率以22.6%首次超过了21.9%的全球平均水平。<br />
<br />
　　今日之互联网，已经成了一个不可忽视的社会舆论场。与此同时，信息传播者的角色也在发生变化：在“单向传播”时代，网络信息相对处于可控状态，而在“互动传播”时代，每一个网民都可以做信息传播者。随着博客、播客、维基以及Twitter等的广泛普及和大量应用，互联网正在成为公众交流信息、表达意见的自由论坛，成为无所不在的民意广场。<br />
<br />
　　安替认为，Twitter的出现，是一次传播方法的革命。“它等于是把信息的传播，从报纸的每天一次、通讯社的每几个小时一次，变成了每分钟一次，完全改变了媒体的竞争格局。”<br />
<br />
　　“通讯形式的加速改变，会导致传播方式的改变，很多形态、心态的改变，就像电报和互联网改变了世界一样。未来一两年，我们会发现，媒体、社会形态都会被Twitter影响。它比以前的很多应用技术，如Facebook 、Blog的革命性还要大。”<br />
<br />
　　其实，信息经济学的研究早就表明，信息和资本、土地一样，是一种需要进行经济核算的生产要素。信息不对称，会造成市场交易双方的利益失衡，影响社会的公平、公正原则以及市场配置资源的效率。<br />
<br />
　　而互联网的文化核心，是信息的自由流动，是用户的全面参与。这是时代的大势所趋。<br />
<br />
 理财一周独家稿件声明</div>

							
							
							
							
