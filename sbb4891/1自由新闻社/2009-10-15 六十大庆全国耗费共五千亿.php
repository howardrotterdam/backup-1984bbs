													<h2>六十大庆全国耗费共五千亿</h2>
						
						
																											<div id="postmessage_282647" class="t_msgfont">据争鸣杂志报导，中共为了大张旗鼓庆祝建政六十周年，整个庆典经费预算高达五千亿元的天文数字字，尚未封顶。<br />
&nbsp; &nbsp;<br />
报导说，中央关于六十周年庆典的经费开支，有两条规则：六十周年国庆庆祝活动经费，属於特事特办特拨，不受国家年度预算限制；六十周年大庆为二零零九年之头等政治、社会大事，要突出的是政治而非经济，要算政治帐，提升国际形象，产生深远影响。<br />
&nbsp; &nbsp; <br />
首都阅兵庆祝活动经费超过七百亿<br />
报导说，据知情人士透露：国庆阅兵、群众游行、焰火晚会、歌舞酒会等，不包含有关工程费用、人员薪酬等开支，预计花费超过七百亿元，比北京市二OO八年度财政总收入的六百六十亿元，还多出四十亿元。<br />
&nbsp; &nbsp; <br />
报导说，国务院办公厅、北京市政府「六十周年庆典筹备办公室」，总参谋部、国防部「六十周年国庆阅兵领导办公室」等部门陆续披露的相关经费支出，以及正在申请追加的经费等资科显示：首都国庆阅兵、庆祝活动的代价，要超过七百亿元，创下新世纪庆典活动经费的世界纪录。其中包括：二OO八年十月，国务院为北京一巾区道路扩建、设施更新，特拨一百二十亿元；二OO九年二月，国务院又为长安大街和天安门整修工程，特拨七十五亿元；二OO九年五月，国务院再又特拨五十亿元，作为参加筹备和保卫国庆期间安全保卫工作的党政部门、公安机关的津贴。<br />
&nbsp; &nbsp; <br />
另外，北京市政府披露：十．一白天庆典、晚上联欢活动的开支，为十亿八千五百万元；各项应急措施装备和五十万协助治安人员上岗开支，为八十八亿元；十万群众游行彩排及道具等项开支，为八亿五千五百万元；新建五十六根巨型「民族团结柱」，造价八千二百万元。<br />
&nbsp; &nbsp; <br />
还有，为配合国庆阅兵和防备突发事件，动用了八千名工程兵，进行一项列为特级的战备工程：整修和加固上世纪六十年代后期响应毛泽东号召「深挖洞、广积粮、要准备打仗」所修建的北京地下城，及地下城连接各主要党政机关建筑物、军事指挥中心的地下通道，面积达二百七十多万平方米，於今年七月底完工，耗资二百一十多亿元，等等。<br />
<br />
北京市委书记刘淇，九月初在全市处局级干部大会上开出有价支票：能完成国庆六十周年期间各项任务的，年终发放特别奖金，表现突出者考虑晋升二级。<br />
<br />
「国庆」烧钱，银子滚滚，不知又要养肥多少贪官，多少高干子弟、皇亲国戚。<br />
&nbsp; &nbsp; <br />
大阅兵专项经费支出<br />
报导说，国防部和总参谋部奉党中央、中央军委人明令筹办的「世纪大阅兵」，是自二零零八年九月初启动的，已经四次增拨经费，总额达一百五十二亿元。先后建了五个集训基地，以及沙河阅兵村、艮乡阅兵整休总部。从二十二万海陆空三军中，经四轮挑选出各兵种九万二千人进入训练基地，进行封闭式操练排演。仅阅兵的服装制作、人员特殊津贴，就达四十亿元。<br />
&nbsp; &nbsp; <br />
另阅兵使用的武器装备，早从二OO六年九月即开始精选，其中特定十二种先进武器装备要在阅兵式中亮相，以展示国防高科技水平。有关装备精选、运装、展示的经费开支，高达一百七十多亿元。<br />
&nbsp; &nbsp; <br />
中央大烧包，省市紧跟上？<br />
另外，报导列出二零零八年度GDP超万亿元省市的六十周年大庆的经费预算：<br />
广东省，四百二十五亿元；<br />
江苏省，二百七十亿元，<br />
山东省，二百八十亿元：<br />
浙江省，二百六十亿元，<br />
河南省，一百六十亿元；<br />
河北省，一百五十五亿元；<br />
福建省，一百四十亿元；<br />
湖北省，一百三十亿元；<br />
上海市，一百二十亿元；<br />
湖南省，一百二十亿元；<br />
辽宁省，一百一十亿元，<br />
北京市，八十六亿元；<br />
四川省，四十八亿元。<br />
<br />
六十大庆全国耗费共五千亿<br />
报导说，三十一个省（区）直辖市的国庆六十周年庆祝活动经费，加上首都北京的大阅兵支出，全国上下为之付出的经济代价是五千亿元。五千亿元是个什么数字？它相等於二OO八年中国财政收入六万五千零五十亿元的百分之七点七，同年国民生产总值二十七万二千亿元的百分之一点八五，以及北京奥运会全部开支五百五十亿元的九倍，它还等於全国十亿农村居民年收入的总和！</div>

							
							
							
							
