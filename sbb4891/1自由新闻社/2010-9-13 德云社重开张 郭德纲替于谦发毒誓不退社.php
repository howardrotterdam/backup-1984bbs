 　　德云社重新开张 郭德纲作揖谢观众<br />
<br />
　　经历一个月停演自查，德云社于9月12日晚7:20正式复演，拉开了德云社系列演出帷幕。由于此次粉丝下手早，开票一小时就把门票抢空，让黄牛党根本无处下手。开幕式上，德云社全体成员踏着锣鼓点上台亮相，也包括经历打记者风波的李鹤彪，却惟独不见近日盛传单干的曹云金。郭德纲一上台先抱拳作揖，“感谢我的衣食父母！”随后，全体演员在郭德纲的带领下，表演了《发四喜》。郭德纲领唱，台上台下一起和着有节奏的掌声唱太平歌词。当老郭唱到《劝学歌》中的“小人过河就拆桥”，台下立刻有大批钢丝应和。<br />
<br />
　　郭德纲替于谦发毒誓 退社五雷轰顶<br />
<br />
　　郭德纲和于谦压轴演出《卖布头》，距离开场亮相间隔了足足两个半小时，但观众们仍旧兴奋不已，一片叫好、尖叫。还没开口，就接连冲上来七八个粉丝送花，还有美女钢丝送水给郭德纲润嗓子。“演员艺字当先，不仅有艺术，还要有艺德。我先说好了，谦哥可不能退出。这样的好搭档百年不遇，不孕不育。我替你启个誓吧，倘若离开五雷轰顶天打雷劈。”郭德纲调侃“退社”，不忘拆掉于谦退路。两人在台上连说了一个小时，还开玩笑：“我不能歇，我一歇场灯就关了。”返场演出中，郭德纲干脆把云字辈最小徒弟、于谦的儿子抱上桌子来一段。这个4岁的小家伙不怯场，不仅能唱《我是一个兵》，还会来一句太平歌词。<br />
