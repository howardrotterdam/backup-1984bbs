																											<div id="postmessage_37718" class="t_msgfont"><a href="http://www.cnbeta.com/articles/77735.htm" target="_blank">http://www.cnbeta.com/articles/77735.htm</a><br />
<br />
据熟悉情况的IDC业内人士透露,由于信产部及上级部门最近展开网站清理及备案专项行动,近期将会有大量网站被关停,一些中小IDC企业的机房、服务器等也将被关闭.<br />
据透露,日前相关部门下发了《关于集中开展全量网站清理及备案专项行动的通知》.通知要求IDC企业将所有于2月24日16:00前尚未获得备案号的网站进行关闭处理;保证网站在获得备案号前一律不得擅自接入等.<br />
<br />
柯雅通信的工作人员透露,这次备案检查将有将近超过10万的网站被关闭,目前已经有12家电信机房因为监管不力而关闭.<br />
<br />
该人士表示,目前网站进行备案需要20天,如果备案信息有错误退回再修改的周期大约60天,而且目前工信部备案网站访问速度缓慢,造成各网站无法在24日16点之前提交网站备案信息,因此这次将有大批量的网站被关闭.<br />
<br />
该人士称,“很多IDC企业也可能会遭遇致命的打击.根据我们的统计,柯雅通信大概需要关闭将近40%的客户网站,其中10%是等待备案的企业网站,其余的30%要么不再做站要么流向了国外空间.”<br />
<br />
“如果客户流失40%,对于一家小的IDC企业来说非常严重”,万网一位市场人士向TechWeb分析称,“虽然这次备案非常严格,但大的IDC企业肯多没有什么影响,洗牌的基本都是中小IDC企业.”<br />
<br />
上述柯雅通信人士表示,目前公司已经对很多客户进行了退款,公司也为后半年发展作出了详细的规划,并且可能会裁减2/3的员工以面对低迷的市场状况.<br />
<br />
但他同时强调,柯雅通信并非个例.“目前来看,因为被关闭机房而宣布倒闭或者发布公告想被收购的公司大概有十余家.”<br />
