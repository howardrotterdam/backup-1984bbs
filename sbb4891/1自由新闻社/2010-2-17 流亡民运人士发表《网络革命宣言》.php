																											<div id="postmessage_483218" class="t_msgfont">《网路革命宣言》<br />
<br />
十九世纪末，日本有“明治维新”，满清有“戊戌变法”，前者成功，后者流产。于是，在日本，至今，最古老的君主制与最现代的民主制共存，那是统治者向民间让步、让权的结果。君主立宪制，这个名词，本身象征着，王权与民权的妥协，历史传统与现代文明溶为一炉。<br />
