刘晓波参与起草《零八宪章》，以理性、温和、谦卑姿态，奉劝中共：与民众和解，共建民主中国，融入文明主流。中共非但不自省，反而将刘下狱，悍然判处刘重刑11年，公然展示权力傲慢。<br />
<br />
关于中国的发展，我们说过：不需要中共做什么，只需要中共不做什么。仅仅是“松绑”，中国经济便迅速成长；如果停止封锁互联网，不再限制新闻自由，不再干预司法独立，一个民主的中国，将自然而然地诞生，瓜熟蒂落，水到渠成。<br />
<br />
中南海拒绝在政治上“松绑”，基于两怕：害怕被清算----历史上，中共杀人如麻、血债如山；害怕失去既得利益----现实中，中共以权谋私，腐败成性。据此可知，将中国的政治改革与民主发展，寄望于中共，无异于缘木求鱼，与虎谋皮。<br />
<br />
重判刘晓波，扑灭《零八宪章》，等于堵死和解之路。这是对改良的蔑视，这是对革命的召唤。言革命，未必就是暴力革命。当代历史证明，颜色革命，以其和平、理性、非暴力、乃至浪漫主义色彩，更蔚然成风，广为流行。<br />
<br />
鉴于中共政权，是当今世界最残暴、最狡诈的厚黑集团，中国的颜色革命，就不大可能是“玫瑰革命”（格鲁吉亚式）、“橙色革命”（乌克兰式）、“郁金香革命”（吉尔吉斯斯坦式）、“天鹅绒革命”（捷克式）等，而注定是网路革命（互联网革命，Internet Revolution）。<br />
<br />
至2009年，中国网民人数达3.84亿；预料2010年，中国网民人数将突破4亿。以中国之人口，中国网民人数，必将遥居世界首位。中国网民，无论其观点如何，至少有一个共同立场，那便是：要说话，要畅所欲言，要尽情表达自己的观点；要最大程度的获取信息，纵览世界，拥抱时代。砸碎网路枷锁，畅游资讯海洋，中国网民，目标一同，起点一致。<br />
<br />
网路，是信息时代的产物，代表人类文明的新台阶：信息公开，信息速递，信息无国界。网路所及，提高了商业效率，增加了施政透明，激发了社会活力，普天下受惠。惟中共腐败集团，竟视之如洪水猛兽，倾党国之力，予以过滤、拦截、堵塞、封锁，先后构建“金盾”、“绿坝”等网路“柏林墙”，培植、安插无数网警、线民、五毛党。绞尽脑汁，耗尽巨资，穷尽人力。<br />
<br />
对抗网路，中共自我定位为文明之敌。反文明，就是反人类。当然，固守专制的中共，本身就是人类公敌。北京独裁政权，早已在国际上陷于孤立，只待从内部予以瓦解。<br />
<br />
在网路上，中共既守且攻。封锁和监控互联网，将中国民众隔绝于信息“柏林墙”下，是为守；对民主国家发动日趋猛烈的骇客攻击，破坏他国政府与国防网络系统，大量窃取他国机密，是为攻。中共发动网路侵略战争，是招致美国反击的直接原因。支持谷歌退出中国，力倡推广网路自由，誓言提供技术支持，帮助中国网民“翻墙”----美国政府的最新姿态，昭示，无可避免的中美世纪大战，已经在网路上开打。<br />
<br />
中南海拼死封锁网路，恰恰暴露，网路，成为中共腐败集团的真正软肋。只要击中这条软肋，貌似铜墙铁壁的中共独裁统治，就随时可能土崩瓦解。<br />
<br />
于是，国人可以群起攻之，各显身手。探寻秘道，翻越封锁墙；分享技术，制造信息井喷；电子邮件，传播真情实况；博客，论坛，聊天室……处处都是战场。无需短兵相接，更无需流血牺牲。以斗室为堡垒，以电脑为阵地，以滑鼠为武器，就可以，随时投入战斗，随时撤出战斗，随时休整再战。可以是游击战，也可以是阵地战，还可以是集团作战。位置不拘，时间不限，手法千变。<br />
<br />
这便是网路革命，这便是“中国特色”的颜色革命。四亿网民，都是中国网路革命的生力军。这场革命，不可能一夕竞功，但若持之以恒，假以时日，必动摇中共统治根基。<br />
<br />
这场革命，不仅限于中国人民，港澳居民可为之，台湾民众可为之，海外华人可为之，国际友人可为之。网路无国界，网路革命无国界。“暴秦无道，天下共击之！”推倒中国“柏林墙”， 当今世界最大的专制壁垒，此其时也！<br />
