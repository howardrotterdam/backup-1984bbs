1984年在得克萨斯州的达拉斯市发生反对里根政府内政外交的民众举行游行示威。示威者中有一名叫约翰逊的先生为了表达不满，义愤难平，点燃了一面美国国旗，约翰逊绕着熊熊燃烧的国旗歇斯底里大叫：“红、白、蓝，我要让你成碎片!”以泄胸中怨气。得州当局很快以有意损坏国旗罪逮捕并起诉了约翰逊。官司曲折而漫长，直打到最高法院。五年之后，最高法院以五比四，一票胜出判定：在公众示威中焚烧国旗是一种受宪法第一修正案“表达自由”所保护的行为。这一判决不但宣告了约翰逊无罪，而且，它使美国48个州和华盛顿特区有关国旗保护的地方法律失效。正如其他具有里程碑意义的司法事件一样，这一判决并非结局，而仅仅是一个开始。众多将国旗视为民族象征的美国人被激怒了。他们绝不允许这一伤害他们民族情感的行为被宣布合法。当时的一项民意调查显示，四分之三的美国人希望用法律来保护国旗。<br />
<br />
1989年10月，国会迅速通过了《国旗保护法》作为对最高法院判决的对抗，然而这一立法也马上遭到了支持焚烧国旗或支持言论自由的人士的抗议。就在这一法律生效当天，一位名叫埃里奇兰的女士就不惜以身试法，在国会山下点燃国旗。美国联邦政府以《国旗保护法》中有关焚毁国旗的罪名指控埃里奇兰。官司又到了最高法院。这时，最高法院的独立性和权威性得到了充分的发挥，再次以五比四的票数宣布《国旗保护法》违宪，重申其焚旗合法之立场。反对焚旗的人士对最高法院的权威不满，他们一计不成又生一计，寄希望于通过新的宪法修正案来保护国旗。但得到的仍然是失望：众议院提出的护旗宪法修正案因未获三分之二多数票而被否决，参议院护旗派的主张也是58票对42票，未获三分之二多数票通过而夭折。但是将护旗等同于爱国的人士却并未气馁，1994年8月，美国65个全国性社会组织在国会山下正式成立全美“公民护旗同盟”，发誓一定要通过宣传、游说，促使国会能有三分之二的多数通过保护国旗的宪法修正案。然而这场运动最终也以失败告终。<br />
<br />
这场美国司法史上的公案进一步向人们显示了美国司法权力的强大。不但对其政府的行为进行核实审查，而且对国会所立之法也有权力宣告其无效。案件的意义不仅仅局限于此，更重要的是美国众多人士对言论自由的保护。言论自由，被认为是所有政治权利中的首要权利。没有自由表达的意志，一切民主自由的制度构建都等同于纸上谈兵、空中楼阁。爱国情感固然可贵，焚烧国旗虽然不妥，但是表达自由的原则却是至高无上的。为了维护表达自由，就必须作出牺牲、容忍国旗被任意毁坏这一事实，承受由此而产生的情感上的代价。<br />
<br />
这需要从美国宪法的第一条修正案谈起。1791年12月15日国会通过并得到联邦最高法院批准：“国会不得制定关于下列事项的法律：确立国教或禁止信教自由、剥夺言论自由或出版自由、剥夺人民和平集会和向政府诉冤请愿的权利。”这一条款已成为美国民众维护自身言论自由的法宝。它并不支持焚烧国旗这一行为，但是却保护以焚烧国旗这一行为来表达自我意志的自由。事实上，这一条款已经超越了它发源的历史范畴，是建设公民社会的一块基石，当然也给生活在专制体制下的人民指引了前进方向。</blockquote></div>转帖<a href="http://blog.ifeng.com/article/2372012.html" target="_blank">http://blog.ifeng.com/article/2372012.html</a></div>

							
							
							
							
