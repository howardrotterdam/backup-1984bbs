																											<div id="postmessage_363168" class="t_msgfont"><a href="http://news.xinhuanet.com/politics/2009-12/09/content_12615024.htm" target="_blank">http://news.xinhuanet.com/politi ... ontent_12615024.htm</a><br />
<br />
据京华时报报道，昨天（12月8日），由人民网主办的“人民喜爱的艺术家”颁奖典礼在人民日报社举行，刀美兰、于蓝、于洋、王晓棠、田华、关牧村、李双江、李维康、欧阳中石等获奖艺术家到场。人民日报社社长张研农等领导向到场艺术家颁发了奖杯和证书，人民日报社副总编辑马利在颁奖仪式上致辞。<br />
<br />
&nbsp; &nbsp; “人民喜爱的艺术家”大型网络评选活动于今年8月启动，遴选了舞蹈、音乐、影视、书画等领域的140名艺术家参与评选。活动共收到网友投票近3000万张，经过网友投票、提名和评审几个阶段，评选结果昨天正式揭晓，彭丽媛、于魁智、成龙、张艺谋等60人获得“人民喜爱的艺术家”称号。<br />
<br />
&nbsp; &nbsp; 著名电影表演艺术家于洋说：“其实受人民尊重的艺术家何止这几十名呢？感谢广大网民对我们的鼓励。我在有生之年一定要做些什么，来回报你们的鼓励。”歌唱家关牧村说：“喉咙长在艺术家身上，艺术生命却存活于大众之中，我将一如既往，以感恩的心面对一切。”<br />
<br />
&nbsp; &nbsp; 人民网昨天还开通了“人民喜爱的艺术家”官网，及时更新艺术家最新资料，发布艺术家的动态信息和作品，并开辟留言区作为艺术家与网友互动的桥梁。<br />
<br />
&nbsp; &nbsp; 人民日报社社长张研农向中国文联党组书记胡振民颁发了人民网艺术顾问的聘书。（记者许青红）<br />
