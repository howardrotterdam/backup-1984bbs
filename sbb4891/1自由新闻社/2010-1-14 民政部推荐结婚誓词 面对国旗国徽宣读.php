																											<div id="postmessage_431642" class="t_msgfont">新闻来源：北京日报<br />
北京日报1月14日报道 新人婚登领证前，可以在主持人带领下宣誓“互相忠实”，认真审视婚姻的责任。昨天，民政部向婚登机构推出4种版本的结婚登记颁证词，供新人婚登领证时宣誓使用。<br />
<br />
民政部社会事务司在“推荐”中强调，此举旨在增强新人婚姻家庭法律意识和责任意识，进一步规范结婚登记颁证仪式。<br />
<br />
有关婚姻专家表示，新人走进婚姻，必须有足够的感情基础，同时也要正确认识婚姻、家庭的责任。“不能一遇到问题就离婚，必须学会去接受、处理双方家庭、长辈晚辈间的矛盾，这样才能成长，并且获得真正、持久的幸福。”<br />
<br />
针对婚姻责任变淡的趋势，新推出的颁证词几乎无一例外地加入了“包容和理解对方”、“相濡以沫”、“同甘共苦”等字眼，希望能让新人更加端正婚姻家庭观。<br />
<br />
记者从市民政局了解到，婚登处也提供证婚服务，但一般只在新人主动提出需求时才会提供此项服务。目前，新人婚登时的证婚需求并不大，普通工作日中，通常十对新人中不到一对新人要求证婚。<br />
<br />
记者询问了十多位待婚族，大家均表示“没有特别的需要”，主要原因是没有亲朋好友见证，仪式感不强，并且担心现场的仪式太仓促。“感觉举行婚礼宴请时候说誓词更合适、更正式。”准备今年“五一”结婚的小胡说，“婚登时颁证词有意思的话，我也会考虑，毕竟是法律意义上真正的结婚。”<br />
<br />
民政部推出规范版本的颁证词后，今后婚登机构有望加大提供证婚服务力度。不过，婚登机构有关工作人员表示，类似9月9日、8月8日等婚登高峰日，婚登新人数量是平时的十多倍、几十倍，很可能只能用集体证婚代替个别证婚服务。<br />
<br />
民政部社会事务司表示，各地民政部门也可以提供更好的颁证词版本，今后将适时补充或更新。<br />
