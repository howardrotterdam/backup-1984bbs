2009-11-24<br />
索马里海盗日前表示同意收取赎金释放中国货船“德新海”号以及船员。中方对事件不予置评，同时限制国内媒体相关报道和网络舆论。自由亚洲电台特约记者丁小报道<br />
<br />
<br />
<br />
<br />
近期索马里海盗发言人表示同意接受三百五十万美元赎金，上月中被劫持的中国货轮“德新海”包括二十五名船员将于下周获释的消息，引起了大陆网民广泛关注和议论。<br />
 <br />
各方意见不一，有的认为人命最重要，交钱放人是好事；也有人认为此举有损强国尊严；有帖子置疑此前派出护航的中国海军能力，甚至与美军营救行动做比较。<br />
 <br />
这一消息周日由官方英文的中国日报以及有海外央视之称的凤凰卫视进行报道，其他的媒体和门户网站全部转载凤凰网的报道。据悉 “德新海”十月十九日被索马里海盗劫持后，大陆各大媒体就收到指令，不得擅自报道相关新闻。　一名北京的记者向本台证实：“这个题目肯定是要控制的。据说是现在私下达成协议了，可能已经给了钱了。” <br />
 <br />
而凤凰网上的海盗接受三百五十万美金将放人这一报道，周二已经没有了，点击搜索连接将会显示：“输入网址有误或该信息已被删除”的提示。而在各大网站论坛上,相关话题的讨论也被降温处理，有大量帖子被删除的迹象。成都专栏作家、媒体评论人士宋石男有这样的观察：“可能觉得还是涉及一些国家的评价，和军事无关，一般网民也谈不到军事机密。看到被删的帖子里面左的右的都被删，比如铁血论坛呼喊着要出兵攻打的也被删；一些偏右的说没用、软弱、保护国民不利的那些也被删，可能主要还是要针对一些情绪。”<br />
 <br />
日前中国外交部例行记者会上，有媒体就赎金放人的进展提问，发言人回应无法提供任何相关交涉的信息。<br />
 <br />
香港城市大学政治学讲座教授郑宇硕认为从人道立场出发支付赎金救人是值得赞赏的做法，中方应该更坦然面对公众：“不管怎样，先把人救回来再说也是符合人道主义的做法，大家也知道以中国国情来说，如果政府不同意，船东是不能这样做的。我觉得既然中国当局愿意这么做，那就清清楚楚说明它的立场，这是正当而聪明的做法。”<br />
 <br />
也有评论分析中方在国庆高调阅兵背景下派出海军迎战海盗，最终却拿钱赎人有伤颜面，故不愿提起，澳门军事评论员黄东说：“最惨的应该是今年海军成立六十周年经过海上阅兵、而且十月国庆阅兵又刚刚展示过自己强大的武力，就在这个时候发生这个事情，非常没有脸面，所以政府不想提，这是一个原因。”<br />
 <br />
与此同时的另一个相关消息，此前成功自行抗击海盗劫持的香港中远货船富强轮，两名负伤广东籍船员日前回到广州，各大媒体做出了英雄模范式的报道。官方工人日报报道更写道：“以两名船员受轻度枪伤的微小代价，换来了价值近两亿元的国家财产的安全。”而广州日报报道结尾不忘加上 “富强轮在中国海军护航舰队直升机的保护下，摆脱了危险。＂<br />
 <br />
黄东认为亚丁湾海域的形势令护航舰难有发挥，与其对海军给予过高的要求，不如做好遇到海盗时自救的训练和准备更为实际：“希望海军护航是不太切合那个海域的实际情况，倒不如增强自己的防御能力，富强轮这个事情证明团结起来抵抗是有用的，靠海军不如靠自己。”<br />
