&nbsp; &nbsp;&nbsp; &nbsp; 背景：近日，由于拆迁问题，几千愤怒的群众连续多日围在通安镇政府门口，向镇政府讨要说法，原因是镇政府涉嫌截留拆迁户的宅基地补偿款以及人头费，群众主要诉求：1之前的几千户拆迁户的宅基费补偿没有拿到（有的说是41万，有说42万，有说38万，大概在40万左右），2，3万的人头费只拿到1万。具体表现为之前每户人家拆迁补偿大概20多万，有的10多万，现在拿到宅基补偿的据说基本上在60-80万，才1-2年3-4倍这是个非常大的差距。有人说以前房价便宜，这样的想法是错误的。因为现在的房价是政府托市托出来的，其他物价以及工资水平都没有这么几倍的增长，拆迁户住的是定销房，拆迁补偿款是给拆迁户补偿生活以及土地的，不是根据房价的变动来补偿的。由于此事涉水比较深，当地电视台都不敢来采访这件事情。另外之前的拆迁也涉嫌非法拆迁，拆迁几年的都没有拿到房产证。<br />
&nbsp; &nbsp;&nbsp; &nbsp; 事情经过：15号白天，镇政府聚集上万群众讨要说法，并且在白天堵住镇书记讨要说法，在被困一天后镇书记承认之前有截留拆迁款，而且推托事情主要是前任领导造成的（前任于09年被群众举报现在吃官司），还坦诚拆迁办以及各村书记都有份。当晚书记打了几十个电话要求拆迁办以及村书记过来说明事情，但是没有一个人出来。在始终没有合理说法的情况下群众继续围住镇书记。其间群众还买了矿泉水，百事可乐给书记喝，始终善良对待书记。并且继续要求政府给出合理的答复，但是除了书记被围困政府始终没有出面给任何答复，政府也没有派人来协商或者谈判。至16号凌晨2.30分左右，突然出现3批特勤手持盾牌和警棍包围镇政府，与手无寸铁的群众对峙，其间群众推翻一辆救护车，也没有人主动去攻击特勤。但是20十分钟后特勤突然开始暴力镇压群众，挥舞着警棍冲向前面的手无寸铁的百姓，把前面逃跑慢妇女和老人用盾牌推倒后一阵毒打，其中有一名年轻男子被特勤拉进特勤的围圈里面一阵暴打，一名女性想冲进去把那面男子拉出来，但是特勤围上去把此女子一阵乱棍直到女的躺倒地上嚎哭才停止。在群众被打散驱散开后特勤护着镇书记扬长而去，之前被逮到的遭遇毒打的几名群众也背带走。<br />
&nbsp; &nbsp;&nbsp; &nbsp; 16号白天，当地居民继续聚集，在华通花园小区的外面的华金路直到附近的312国道附近聚集几千人讨要说法并且要求释放被抓的群众。其间始终没有政府领导出面给群众解释或者跟群众谈判。至傍晚6，7点钟聚集群众越来越多，隔壁浒关镇的群众也向华通小区聚集，到晚上要有上万人聚集在一起，特勤也达到上千人。其间特勤又几次把靠近的群众围进去暴打一通，并带走若干群众。到10点多特勤手持盾牌和警棍扩大他们的围圈，开始驱赶群众，有一人什么话也没说什么事也没做就因为走得比较慢遭被一阵乱棍猛打直到躺在地上动弹不得，由于马路被特勤封住，群众无法把伤者送去医院。在对峙几十分钟，群众强烈要求马上送医院的情况下，几个警察估计害怕出人命，把伤者拖上特勤后面的车子，不能确认是否送医院还是在救护车临时救治。随着对峙的继续，有群众在小区里面扔了几个爆竹到路上，这时几十个特勤冲到小区护栏边上，把护栏外面的毫不相干的几个年轻人围着一阵乱棍，其间群众不断呼喊打错人了，年轻人站在外面什么事都没有做，但是暴徒毫不理睬群众的呼喊，把几个年轻人推进特勤的围圈又是一阵乱打，然后把伤者拖到后面的车里。全副武装的暴徒面对手无寸铁的群众，毫不手软主动攻击群众甚至不分清红皂白连不相干的年轻人也不放过。其间很多群众呼唤记者到现场，但是始终没有记者愿意过来，到12点左右群众无可奈开始慢慢撤离。<br />
&nbsp; &nbsp;&nbsp; &nbsp; 深思：其实中国人都是善良的人，只要政府不是把人民逼上绝路，纵使明知官员贪污也不会起来造反。只是这次政府把群众的活命钱都吞了才会讨要说法。但是这两天的对峙中，政府始终没有出面解释与群众的矛盾，也不想用和平的方法处理此次事件，只是一味的出动防暴队暴力镇压手无寸铁的群众，连老人妇女也惨遭毒手。自称为人民政府的流氓政府暴力对待善良的百姓，人民警察打人民，连旧社会应该都很少出现。<br />
&nbsp; &nbsp;&nbsp; &nbsp; 目前周围的东渚镇浒关镇群众也开始起来讨要说法，只是手无寸铁的群众只能被挨打却毫无还手之力。请有良知的人们关注通安拆迁事件，也许明天群众还会自发聚集，希望有良心的媒体早点关注制止暴徒对通安人民的卑劣行径，相信暴徒在你们的镜头下不会再做伤天害理的事情。<br />
<br />
