&nbsp; &nbsp; <br />
&nbsp; &nbsp; 25日，各小区在贴出虎丘公安分局的公告后，又张贴了苏州市公安局的通告，并将通告发放到户，通告称将依据《中华人民共和国刑法》、《中华人民共和国治安管理处罚法》对抗议活动涉嫌犯罪的人员进行查处，该通告和虎丘分局的公告一样都没盖公章。由于力量对比发生逆转，通安、东渚24日及25日白天均未发生上街抗议事件，但据不完全统计：枫桥镇的何山花园，24日夜有民众上街，马涧商业城也有上千民众聚集。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 各小区除宣传小组入户要求居民签字保证不上街之外，还组织党员在小区内四处走动，对进出的居民都进行监控。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 7月24日，各学校学生家长都收到学校通知，要求管好学生，不要上街，其中通安中学的通知称：“尊敬的各位家长，您好！通安事件已演变为有境外敌对势力介入，利用群众诉求，在通安、浒关等地煽动、组织违法活动，请各位家长认清形势，坚定立场，同时教育孩子做到不参与、不围观、不传谣、不跟帖，以免给自己及孩子造成遗憾！”<br />
