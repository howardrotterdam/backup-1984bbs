2010年07月20日 06:51财经网<br />
【《财经》记者 鄢建彪】因怀疑征地补偿款被克扣，苏州市高新区通安镇连续数日来出现村民大规模聚集事件。<br />
<br />
7月18日晚19时许，通安镇上万村民再次聚集在G312国道附近，并试图阻断交通，阻止过往车辆通行。十多分钟后，由各地调派的近千名特警和防爆警察陆续赶到，将村民驱散隔开，阻断在通往国道一条公路上。<br />
<br />
《财经》记者在事发现场看到，当时对峙双方并未发生激烈冲突。在僵持了近3个小时后，21点40分，聚集群众陆续离去。<br />
<br />
此番村民大规模聚集，盖因“征地补偿款”而起。由于此前镇政府拒绝了村民的相关诉求，在连续几天的冲突之后，随着双方的对立心理加剧，最后出现万人堵路的局面。<br />
<br />
聚集民众几乎都是来自通安镇华通花园小区的居民。华通花园共有四期，为通安镇下辖各村落农民的安置房，共有2-3万人在此居住。<br />
<br />
2003年以来，苏州市高新区以建设各类工业园的名义，陆续向下辖村镇拆迁征地。2010年初开始，新一轮征地再次启动。<br />
<br />
据通安镇新钱村村民介绍，在这次拆迁中，同等面积补偿金额超过2008年以前的3倍以上，这其中包括每户近40万的土地宅基费。而2008前并无此项补偿款。村民们举证指控该笔款项被镇、区政府工作人员克扣、吞并，并屡次向有关部门反映，并未有任何下文。<br />
<br />
与此同时，今年7月初，与通安镇相邻的浒墅关镇的一村民宅基地地块，以每平米6473元，总13.12亿元的高价拍出。与相对较低的拆迁补偿费用相比，通安镇村民认为当地政府假借开发之名，实则是倒卖土地。<br />
<br />
7月14日，上千通安村民聚集在通安镇镇政府，要求得到解释。沟通期间，由于有镇政府领导态度强硬，并表示绝无可能补偿宅基地欠款，闻讯的村民情绪激动，打砸了部分办公设备，并在此后两日内占据该镇政府办公室。<br />
<br />
7月16日晚，镇政府表示每户可以安排一人到通安中学协谈。但是，据新钱村村民介绍，当晚并没有镇政府领导出现，而赶到现场的防暴警察与群众发生冲突，部分群众被打伤，有村民并带走。<br />
<br />
7月17日，包括受伤村民在内，数千名群众聚集在G312国道上，阻断交通，被警方驱散。到7月18日时，堵路行为再次发生，群众数量却增至万名。有村民担心，事件如未获妥善解决，类似情形仍会发生。<br />
<br />
据《财经》记者了解，2003年以来，苏州高新区在开发名义下的征地行为备受诟病，涉及土地征用规模、土地补偿金以及土地征收文件，数年来拆迁户上访不断。目前，在已被征用的大片区域，多地荒草丛生，并未进入实际开发。</div>

							
							
							
							
