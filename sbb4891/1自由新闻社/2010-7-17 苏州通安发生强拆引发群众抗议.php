&nbsp; &nbsp; <br />
&nbsp; &nbsp; 记者采访了几名最近参加过抗议的民众，他们普遍反映，在这种压力之下，说不怕是假的，但也没有怕到要放弃的地步，政府对拆迁补偿没有任何说法，无论如何老百姓是不会接受的，即使今天没人上街，明天呢，后天呢？虎丘高新区的居民肚子里憋着一股气，如果政府不给一个公正解决，事情不会就这么结束。<br />
