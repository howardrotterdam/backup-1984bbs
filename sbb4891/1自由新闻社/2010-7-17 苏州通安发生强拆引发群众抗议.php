　　昨天，虎丘区委、区政府决定，暂停通安镇今年下达的动迁项目和动迁工作，对拆迁补偿标准执行和群众诉求处置不当的通安镇党政主要领导就地免职。据有关知情人士透露，被就地免职的通安镇党政主要领导分别是镇党委书记王军和党委副书记、镇长孟晓瑜。<br />
