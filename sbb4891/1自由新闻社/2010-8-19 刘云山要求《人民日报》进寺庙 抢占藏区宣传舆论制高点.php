													<h2>刘云山要求《人民日报》进寺庙 抢占藏区宣传舆论制高点</h2>
						
						
																											<div id="postmessage_789272" class="t_msgfont">《人民日报》藏文版创刊一周年座谈会在京举行<br />
中国文明网　<a href="http://www.wenming.cn" target="_blank">www.wenming.cn</a>&nbsp; &nbsp;2010-08-19&nbsp; &nbsp; 来源: 中国新闻出版报 <br />
<br />
刘云山作出重要批示<br />
要进一步扩大覆盖面 改进丰富报纸内容 增强吸引力和影响力<br />
<br />
8月18日，《人民日报》藏文版创刊一周年座谈会在京举行。座谈会总结了《人民日报》藏文版创刊一年来的工作经验，并听取各方意见建议，研讨进一步改进提高的措施。中共中央政治局委员、中央书记处书记、中宣部部长刘云山为《人民日报》藏文版创刊一周年作出重要批示。刘云山在批示中指出，《人民日报》藏文版创办一年来深受藏区干部群众欢迎，要进一步扩大覆盖面，改进丰富报纸内容，增强吸引力和影响力。<br />
<br />
中宣部副部长蔡名照传达了刘云山同志所作的重要批示。人民日报社社长张研农、新闻出版总署副署长李东东出席座谈会并讲话。座谈会由人民日报社总编辑吴恒权主持。<br />
<br />
结合学习贯彻刘云山同志重要批示精神，蔡名照对《人民日报》藏文版的工作提出了四点意见。第一，努力把《人民日报》藏文版办成宣传中央精神的重要阵地，传播国内外信息的重要平台，促进民族团结的精神家园，开展反分裂斗争的舆论高地。第二，创新内容、创新形式、创新手段，进一步提高报纸吸引力、感染力。一定要按照刘云山同志提出的“进一步扩大覆盖面，改进丰富报纸内容，增强吸引力和影响力”的要求，针对藏区特点、适应藏族读者需要，丰富内容形式，提高质量水平，办出特色，办出风格。第三，珍惜人才、爱护人才、用好人才，切实抓好藏文版人才队伍建设。要注重培养使用西藏和四省藏区本地的藏族编辑记者，充分发挥他们的重要作用，为他们施展才华、成长成才搭建平台。第四，扩大范围、提高时效、加强配送，努力扩大《人民日报》藏文版的发行覆盖。确保《人民日报》藏文版在西藏和四省藏区能够进机关、进社区、进学校、进企业、进村镇、进寺庙，覆盖到藏区的各个角落。同时，《人民日报》藏文版也要积极探索运用网络、手机等新技术来扩大传播覆盖，提高传播效果。<br />
<br />
李东东代表新闻出版总署对《人民日报》藏文版创刊一周年表示祝贺，并向投身少数民族文字党中央机关报事业，为《人民日报》抢占藏族地区宣传舆论制高点倾注心血的全体员工，致以诚挚的问候。<br />
<br />
李东东指出，创办《人民日报》藏文版，是一项政治性、政策性很强的任务，也是一项新的开拓性的新闻宣传工作。一年来，在党中央的支持下，在中宣部的指导下，人民日报社和西藏日报社精诚合作、共同努力，交出了一份令人满意的答卷。李东东强调，《人民日报》藏文版作为我国第一份用少数民族文字出版发行的党中央机关报，对于把党的声音传遍藏区千家万户，不断提升《人民日报》的传播力、引导力和影响力；对于进一步宣传党的理论和路线、方针、政策，不断打牢干部群众团结奋斗的共同思想基础；对于进一步扩大西藏宣传阵地，抢占宣传舆论制高点，掌握涉藏舆论斗争主动权都具有重大意义。<br />
<br />
李东东对《人民日报》藏文版的发展提出了几点要求。她希望《人民日报》藏文版在已取得成绩的基础上，大力宣传社会主义制度和民族区域制度的优越性，大力宣传西藏和其他藏族聚居区经济发展、社会稳定、民族团结、人民幸福的好形势，大力宣传藏族文化保护和发展的积极成果，大力宣传藏族聚居区民族团结进步的精神风貌。进一步加强《人民日报》藏文版翻译、编排、出版等业务人员队伍建设，特别是要根据藏文报读者的特点，加强翻译工作，在贴近群众语言特点的基础上，不断增强报纸的亲和力、吸引力、感染力。进一步结合西藏和其他藏区的实际，突出少数民族文字报纸特点，形成独特风格，不断增强报纸的可读性，真正把《人民日报》藏文版办成藏区广大读者喜闻乐见的报纸。<br />
<br />
张研农在总结讲话中指出，中央的重视、领导的关心、读者的期望，为进一步办好《人民日报》藏文版提供了重要指导，增添了新的动力。张研农强调要从贴近藏区实际，改进报道内容；整合各地资源，实施采编联动；理顺工作环节，争取发行时效等三方面进一步做好工作。<br />
<br />
会上，来自西藏、青海玉树的读者代表也纷纷发言，肯定了《人民日报》藏文版发行一年来所发挥的积极作用，表示《人民日报》藏文版的出版发行对藏民深入了解党中央的民族政策、宗教政策等发挥了重要作用，并希望《人民日报》藏文版能够进一步扩大发行量以满足基层群众的需求。<br />
<br />
西藏自治区党委常委、宣传部长崔玉英，国家宗教事务局副局长齐晓飞，中宣部新闻局局长张小影，中宣部出版局副巡视员张凡，中央统战部七局副局长沈阳，国家民委文化宣传司副司长马继祖，四川省委宣传部常务副部长侯雄飞，云南省委宣传部副部长伍皓，西藏自治区党委宣传部副部长韩向阳以及《四川日报》、《西藏日报》、《青海日报》代表等出席。（李雪昆）</div>

							
							
							
							
