													<h2>影帝远赴黑龙江吃了一小碗米饭</h2>
						
						
																											<div id="postmessage_410955" class="t_msgfont">温家宝赴黑龙江看望群众<br />
来源：中国政府网 2010年01月02日<br />
　　群众生活得好，我们就放心<br />
<br />
　　――温家宝元旦赴黑龙江看望群众<br />
<br />
　　隆冬的松嫩平原近来遭遇罕见的低温天气，白天最高气温都在零下二十摄氏度以下，天寒地冻，滴水成冰。<br />
<br />
　　元月1日至2日，中共中央政治局常委、国务院总理温家宝来到黑龙江省大庆市和齐齐哈尔市，冒着严寒，登上钻井平台，深入企业、社区和农户家中，亲切看望慰问广大干部群众，代表党中央、国务院向大家致以新年的问候。<br />
<br />
　　大庆油田是我国工业战线的一面旗帜。温家宝对大庆有着很深的感情，2003年以来曾两次到大庆考察。1日12时许，温家宝一下飞机就驱车直奔大庆油田，看望“新时期钢铁钻井队”15152钻井队的工人们。<br />
<br />
　　白雪覆盖的大地上，到处是耸立的钻塔和抽油机。15152钻井队作业现场，40多米高的塔尖上旗帜飘扬，工人们正在平台上紧张忙碌着。这支钻井队继承发扬大庆精神和铁人精神，钻井施工能力连年提高，2009年钻井进尺突破十万米，创造了大庆油田新纪录。温家宝登上钻井平台，和工人们亲切握手，嘘寒问暖。他走进司钻房，在操控台前坐下，右手紧握控制手柄用力向前推，“钻井平台以前是&quot;刹把子&quot;，现在是机械化，比原来好多了”。<br />
<br />
　　总理的到来，让现场沸腾起来了。温家宝向围拢过来的工人们致以新年问候。他说，这是我就任总理后第三次来到大庆，心里非常高兴。在这片黑土地上，几代大庆人顽强拼搏，不仅给国家开采了20亿吨的石油，而且形成了一种战无不胜、攻无不克的精神，这就是大庆精神！大庆人之所以了不起，就在于50年来一直坚守这种精神。今天，在国际金融危机的严重冲击面前，我们仍然需要这种精神，需要创新，需要拼搏，需要奋斗。他鼓励工人们继续发扬大庆精神，努力建设一个百年油田。<br />
<br />
　　已近下午1时，温家宝来到板房中的职工“餐厅”，和大家共进午餐。简陋的餐厅经过简单装饰，洋溢着节日的气氛。温家宝端起盘子，盛了些土豆炖鸡、豆腐炖白菜、炒油菜和一小碗米饭，和十多位工人挤坐在一张桌上，边吃边聊。从工作生活到国际油价，从钻井技术到大庆创业史，总理和大家亲切交谈，餐厅内不时传出阵阵笑声。<br />
<br />
　　临别时，温家宝还走进伙房和宿舍，看看灶台，摸摸被褥，察看职工们的伙食和住宿情况。此情此景，让钻井工人张齐山十分感动，他说，天这么冷，总理还冒着严寒来看望我们。这既是关心，更是鼓舞。<br />
<br />
　　下午，温家宝参观了大庆油田历史陈列馆，并亲切看望了当年曾参加“石油会战”的马德仁、薛国邦及“新时期铁人”王启民等石油战线劳动模范。总理动情地说，你们是大庆人的代表，大庆精神在你们身上传承。无论大庆怎样发展，都是从50年前头顶蓝天、脚踩荒原起步的。我们不能忘记过去的艰苦历程和优良传统，不能忘记那些为大庆建设和发展作出贡献的人们。大庆精神不能丢。没有这种精神，就没有今天的大庆，就没有大庆的未来。<br />
<br />
　　资源型城市转型是温家宝一直关心的问题。近年来，大庆在这方面作出了尝试，大力发展服务外包产业。1日下午，温家宝来到汇聚上百家企业的大庆服务外包产业园，先后走进华拓数码科技有限公司、纳奇网络开发有限公司等企业的工作平面看望员工。看到企业发展迅速，职工朝气蓬勃，温家宝十分高兴。他说，大庆面临两大任务：一是如何充分挖掘资源开发和利用的潜力；二是着力培育接续产业，包括高新科技、农产品加工、服务外包和文化创意等。资源会枯竭，而人的智力永远不会枯竭。新一代大庆人一定要发扬老一代无私奉献、顽强拼搏的精神，把这种精神用在创新上，用在智力的发挥上。大庆的未来是光辉灿烂的。<br />
<br />
　　温家宝十分关心改善民生的情况。在大庆期间，他来到东城领秀安居小区，先后走进居民段贵林和姜晓强两家的新房子，从卧室到厨房，一个一个房间地仔细察看。看到石油工人的住房条件得到了改善，温家宝十分高兴。他说，住房和社会保障对工人来说都是大事，只有把这些事情解决了，工人才没有后顾之忧。应对国际金融危机，更要重视民生问题。我把这件事情看得更重。民生问题不仅是住房和社会保障，还要使职工的收入不断增加。<br />
<br />
　　晚上8点，温家宝又来到大庆市黎明社区，看望正在社区活动中心参加文体活动的居民。总理走进一间间活动室，向大家问好并亲切交谈，还饶有兴致地欣赏了居民的民乐、声乐表演，和他们一起打乒乓球。在老年人照料室，温家宝坐下来，和大家聊起家常。老人们告诉总理，国家连续6年提高企业退休职工养老金，生活一天比一天好。温总理听后说，群众生活得好，我们就放心，就高兴。我们最大的希望，就是大家生活一年比一年好。今天是新年，我给大家拜个年，祝大家健康长寿。<br />
<br />
　　温家宝惦记着广大农民群众。2日上午，他来到位于扎龙湿地附近的林甸县三合乡胜利村看望村民。气温已到零下二十八摄氏度，寒气逼人。温总理踩着雪，在村子里走了十多分钟，察看村容村貌。在杜瑞芬家的院子里，几位村民正在用稻草编草席，总理走过去和他们亲切地打招呼。在王宝库家，他向全家人问候新年。从农家乐经营到收入情况，总理问得很仔细。“种水稻了吗？”“种了，亩产能到1200斤。”总理随即告诉王宝库一个好消息：“今年水稻最低收购价格还要再提高。”他又叮嘱随行的有关部门负责人：“这个信息要早一点告诉广大农民。”温家宝说，缩小城乡收入差距，要从两个方面入手，一是提高和改善农民工的待遇，二是逐步提高务农人员的生活水平。<br />
<br />
　　新合家园是齐齐哈尔市的一个棚改小区，已有1470户棚户区居民住进了这里。2日上午，温家宝来到这里看望居民。听说总理来了，大家闻讯赶来，簇拥在总理身边。温家宝向大家致以新年祝福。他走进居民王德才家，看到老人的新居阳光灿烂，整洁干净，脸上露出欣慰的笑容。总理告诉这位从企业退休多年的老人，如果国家财力可以，还会继续提高企业退休人员养老金。老人高兴地笑了。<br />
<br />
　　齐齐哈尔二机床（集团）有限责任公司是我国重型机床及锻压设备生产基地。2日上午，温家宝走进重装车间看望一线工人。企业负责人告诉总理，国家装备制造重大科技专项有20个项目，他们就承担了7个，现在正全力以赴制造国产大飞机等的相关部件。温家宝说，不要小看一个螺丝钉、一个焊点，它的好坏体现着一个民族的素质。他寄语企业职工和技术人员，新的一年里，要用心制造，用智创造，在关键技术和部件上有所突破。希望企业在创新方面不仅走在全国前列，更要走在世界前列。“我们一定努力。”人们齐声回答……</div>

							
							
							
							
