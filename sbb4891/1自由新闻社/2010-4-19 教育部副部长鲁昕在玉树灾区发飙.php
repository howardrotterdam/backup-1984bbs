1楼<br />
今天下午，教育部某女性官员领导一行在玉树州几所被毁学校考察时，发出累人话语。<br />
首先，该女官员一行穿着笔挺，昂首阔步进入被毁学校慰问受灾师生，下达命令，要求：所有学校十天之内必须开课，否则撤去玉树州教育局局长职务。<br />
