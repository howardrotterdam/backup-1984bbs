还有，这一行人随行多部越野车，车内带有饮水及食物，但面对这些受灾教师时，却没有拿出哪怕一瓶水。（灾区实际情况是，90%的人到现在没有收到救灾的饮水、食物、衣物、棉被等任何救援物品）<br />
最后，该一行人又乘坐越野车离开，离开时还要求受灾教师喊出艰苦奋斗、保证学生上课的口号，为随行摄像师拍摄，以便回去做秀！<br />
作为一个玉树人，我就纳闷，在这样一个时刻，这些所谓的部委领导（暂且不论教育部是不是中国最垃圾的部委），居然跑来下这样的命令，做这样的秀。而我所知道的真实情况是，灾区人民没水、没粮，很多救援物资无法公平发放，而在物资刚运达后就被灾民蜂拥哄抢，很多人都在昨天夜里冻病，我希望国家有关部委，不是要跑出拍段镜头，喊些口号，而是真正为救灾出一份力。他们却衣、缺水、断粮、没有帐篷，已经在低温环境下生存了40个小时，他们需要更多的关注，而不是作秀！<br />
