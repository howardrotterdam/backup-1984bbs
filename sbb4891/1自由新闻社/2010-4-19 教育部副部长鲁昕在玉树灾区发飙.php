													<h2>教育部副部长鲁昕赴玉树慰问灾区师生</h2>
						
						
																											<div id="postmessage_571191" class="t_msgfont">教育部副部长鲁昕赴玉树慰问灾区师生<br />
2010年04月19日08:28&nbsp;&nbsp;来源：人民网-教育频道<br />
<br />
　　人民网教育频道北京4月19日电 （记者林露）昨天，记者从教育部网站获悉，4月14日至16日，教育部副部长鲁昕受教育部部长袁贵仁委托，带队赴青海省玉树州玉树县地震灾区，慰问师生员工，考察了解教育受灾情况。<br />
<br />
　　据了解，鲁昕先后深入玉树州职业技术中学、玉树州民族中学、玉树州红旗小学、玉树县第一完全小学、玉树县第三完全小学、玉树县第二民族中学等学校，向灾区学校校长、教师和学生转达了国务院领导的亲切问候，详细了解学校灾损情况和灾区教育抗震自救情况，研究帮助协调解决相关问题。<br />
<br />
　　鲁昕在与青海省、州、县三级教育系统有关负责同志进行认真商量后，对近期有关工作提出了几点建议：一是要按党中央、国务院要求，全力抢救受困师生。二是教育系统要在当地党委、政府领导下，抓紧抓好当前抗灾自救工作。要即速了解教育系统受灾和师生伤亡情况，全力抢救被掩埋师生，救治受伤师生；注意震情变化，做好师生转移、疏散，尽最大努力减少伤亡；加强指导和推进校舍安全建设，对库区学校可能面临的危险及早编制预案，防止和减弱次生灾害带来的损失和危险。三是按国务院领导指示，带着责任、带着感情，尽快提出灾区学校复课方案，并认真组织落实各项有关工作，教育部将全力帮助协调解决复课过程中存在的困难。四是按照省抗震救灾指挥部统一部署，做好教育恢复重建总体规划和分年实施计划。<br />
