													<h2>公民调查日记（连载四）</h2>
						
						
																											<div id="postmessage_69177" class="t_msgfont">09.04.05(2009-04-05 22:48:36)<br />
一、<br />
<br />
2009.4.4<br />
中午12时许，拨通了成都市公安局王局长电话。我们说：在4月1日晚间，被十数名警察带到青羊区汪家拐派出所被问话，包括很多个人用品在内的物件被没收，但并没有依据法律出具物品扣押清单。王局当即表态：这种行为是违法的，即便是犯罪嫌疑人的物品在被扣押时也需当场出具物品清单。但电话内无法详细听取情况反映，请按照法律程序走，可去该派出所上级单位成都市公安局青羊区分局法制科反映和申诉。<br />
我们遂通过114得知该处电话，拨打过去。对方说：请到位于成都市汉江路13号的分局来当面呈述情况。<br />
13：47，成都市公安局打来电话，说：领导指示有人反映情况，打来电话问询有何反映。问其姓名，未置答。称：我来询问你，是想为你提供帮助，你可以考虑是否反映，如不需要也可挂掉电话。我们赶紧将情况复述一遍。对方说：请稍等，我们了解一下情况后将给你电话答复。<br />
约 15分钟后，电话打来，对方说：据我们了解后，你们的东西并没有被扣押，而只是在进行了解、调查，待调查清楚后，再行决定是否需要扣押，如需要，到时即会给你们开具扣押清单。我们问：我们的东西已经被带走超过48小时，那怎么样才可以被认定为是“扣押”？调查需多少时间？何时能够认定是否为“扣押”？我们的东西现在哪里？对方说：不方便说，你们可以去青羊区分局法制科反映，认为其具体违规、违法行为可以进行申诉。<br />
14时许，来到青羊区分局，法制科一名警官接待我们。了解情况后，对方说：你们的反映如属实，这种行为肯定是违反法律和规定的。你们先坐，我现在先打电话了解一下情况。<br />
十几分钟后，警官回来，说：据我了解，你们的东西并未被扣押，只是在进行了解和调查，如无问题，很快就会还给你们。我们说：按照法律，当时应该现场出具扣押清单，请先补给我们清单。警官说：现在我这里不能给你解决这个问题，东西是你们的，为什么被没收你们自己也清楚，东西是在汪家拐派出所扣押的，我们也无法对其进行指挥，建议你们去找汪家拐派出所。我们说：汪家拐派出所说请等鉴定，至于时间不确定，所以我们找到成都市公安局，成都市公安局说你们可以去青羊分局反映和申诉，来到这里后，又让我们回去找派出所，到底哪里可以解决这个问题。我们只是要求能够依法得到一个清单而已。警官说：我们已经对市局进行了回复，同时也和汪家拐派出所了解了情况。我个人认为，按照公开的法律和我们的内部规定，他们带走你们的东西都是不违法的。我这里解决不了问题，你们可以找派出所，也可以找任何人，也可以走法律程序。情况就是这样。<br />
我们要求见今天值班的局领导，警官说：局长的办公室有牌子，你们可以去。最后警官还说：你们真能给我们出难题。<br />
我们即找遍楼内局长办公室和数位副局长办公室，均无人。<br />
现在看来，情况基本就是这样：如果我们想要要回我们的东西，可以等待警方给我们电话，如果会打的话，那么也许明天，也许明年，这个电话将会告知我们的东西是否被了解、调查清楚，是否可以被认定为扣押.若是扣押，就会给我们出具清单，假如不是扣押，那么我们很快就会收到给我们的邮包了，里面装着我们在2009 年4月1日晚被带走的东西。<br />
今天至此。<br />
<br />
二、<br />
<br />
2009.4.5<br />
上午10时许，我们来到汉旺。东汽门前的钟塔刻针定格在14：28分上。<br />
这座长方体被认为是地震的代表形象，不少游人在钟前合影，有的像是一家子，远远看去，不知喊没喊“茄子”，但脸都笑成了花，有人举着习惯的V形手势。<br />
有当地人和我们说：地震后，东汽中学（汉旺镇唯一的中学）和小学的原学生有迁往德阳，有迁往绵竹，现本镇没有学校。原学校遗址在居民区。<br />
来到居民区，看到有铁网围着，三位女民警在通往受灾最为严重的地段口上站着，她们说：现在里面还不具备让游客进去参观的条件，请大家止步。旁边黑板上贴着绵竹市公安局的通知，大意为：因震区可能会出现山体滑坡、二次倒塌等情况，故不具备进去参观和条件，从4月1日起暂时关闭。<br />
有一位老太太问民警：那我们想祭奠和悼念应该怎么办？民警说：可以上山，有“万人坑”公墓，可以去哪里。至于如何去，我们也不清楚，可以问一下当地人。老太太又问：有多少遇难者。民警说：既然叫“万人坑”，可能就是10000人左右吧。<br />
我们找了一辆摩托三轮上了山，来到在建的遇难者群墓。我们问在铺设路面的工人师傅：这里面埋有多少遇难者？师傅说：八九千，一万吧。问他如何知道，他说：我是本地人，埋得时候我就在。<br />
巨大的墓在山腰间的开阔地上，没有名字，没有碑铭，当然，这些可能都在建。我们送上一束花，鞠躬。师傅说，昨天清明节，这里到处是人。<br />
今天，这里很安静，站在墓前，能听到远远师傅铁钎在铲地，风吹墓前香火轻烟，身上和心里都觉得凉。<br />
一路断壁残垣，这里变成了一座空城。走在这座曾经发达的镇子里，我们看到的有警察，有商贩搭棚子卖花，有游客摆泡斯拍照。还有不多的沉默的本地人，他们说，旧的汉旺将作为地震遗址保留，新的汉旺在不远的地方新起。<br />
<br />
我们问：地震中遇难的学生有多少人。均答不知。<br />
逝者在土中长息，名字不知在哪里封存。相信有一天他们会一一被所有的人得知和忆起。在此之前，即使暂时，也请不要再使用哪该死的三个字了：万人坑。<br />
今天至此。<br />
<br />
公民调查李肺<br />
<br />
<br />
<br />
我已经不好意思再说，昨天我们又被绵竹市公安局刑警大队拘留了十一个多小时，这实在是太侮辱我们的智慧及国家的法律了。<br />
<br />
这个国外太伤我的心，我终于可以体会：感觉已经爱上了这个地方，是多么不容易的事啊！<br />
<br />
昨晚十点我们已经是第二次被强行由绵竹遣送到了成都。由于长时间的人身自由受限制，加上各种威胁和恐吓，我们的精神已经极度恍惚，今天只好暂留成都休息一天，打算明天再返回绵竹，继续了解遇难学生名单。<br />
<br />
我见过从良之后的小偷、骗子和杀人犯，他们在善良的人的面前，都懂得什么叫廉耻。<br />
<br />
我一定要让这些，即使是再愚蠢警察，每当再看到我们的时候，他都要觉得不好意思。<br />
<br />
广东公民，瑞豪4月3日于成都。<br />
<br />
附：刑警大队里的民警向小黄透露，除了汉旺那边的学校以外，金花地区的学校受灾也很严重。<br />
<br />
三、<br />
<br />
政府提供的名单隐瞒了很多。我在博客上看到关于绵竹的名单不是很多，也许你们对绵竹的消息知道甚少，东汽中学就是个豆腐渣工程，死伤的学生老师很多，我表妹班上只活了3个。在地震后的救援工作领导上绵竹市政府应该付最大的责任，那些所谓的领导很自私，在两栋倒塌的楼里，先救几乎没有救命声的那栋却不救救命声很高还可以看到孩子稚嫩的脸的那栋，你们知道为什么吗？就是因为那栋里有领导的孩子！我表妹因为没有得到及时的救援是窒息而亡的，她走的很......<br />
<br />
在过去的一年中，灾后重建上，那些领导也得到了很大的实惠，他们都成了富翁，在其他地方可能也一样吧！<br />
<br />
总之，感谢你们这么做，我向你致敬！</div>

							
							
							
							
