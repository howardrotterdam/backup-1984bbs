<br />
五、<br />
<br />
今天是愚人节。<br />
我和赵同学、徐同学昨天晚上商讨今天是否能够用有创意的方式愚弄对方。在今天，我认识到在愚人这方面，我们很难比什邡警方更有创意。<br />
<br />
上午10：40，我们离开什邡矿业招待所，按照原计划去见一位遇难学生家长。考虑到给该家长打电话无异于直拨110，我们决定找一个公用电话。<br />
在一家药店门口，赵同学用公话拨通家长号码，在她通话时，我想起她说嗓子最近一直不舒服，就进药店内购买消炎药，药店职工很热情，告诉我阿莫西林没有头孢克月亏胶囊管用，我即听从医嘱。扭过来，我在店内饮水机倒了一杯热水。<br />
我左手拿药，右手端水走出药店，心想这样一定会让赵组长感受到团队的温暖。可是却看不到他们两位，我作出的判断是他们给我的愚人节玩笑，这有创意吗？即给赵x打电话，两遍不接。随即打来，说：我在四川，随后联系。我心知不妙，果然收到赵短信：阴天。我问公话老大爷：打电话的人呢？答：公安局的人带走了。<br />
调查以来所有的资料均在我包内，包括学生、家长提供的原件和我们备份的复印件。赵电话过来：在派出所，警方态度还较温和，无不礼貌行为。但就是不让走，只是一直让坐一会再坐一会，我们请你们来，就是坐一会。<br />
到目前为止的情况就是这样：赵和徐被无任何理由的要求在派出所“坐一会”，我在这个小网吧记录什邡警方的这个精彩玩笑。<br />
<br />
我们有思想准备，在这个一年前饱受创伤的城市，发生任何事情都不奇怪，因为在这里，法律、权利等等也许更加不属于我们。<br />
