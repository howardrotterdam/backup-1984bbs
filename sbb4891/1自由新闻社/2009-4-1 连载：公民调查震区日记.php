<br />
什邡新院村家长xxx儿子xxx在512地震中于洛水中学遇难，成绩全班第一。xxx为我们提供了洛水中学当时的官方名单。他说名单上面家长的电话都被监控，全部换了号码。就在见到我们之前还接到监控方电话问其在干什么。他说愿意公开电话号码，有很多人已经不能再次生育，还有住房问题。&quot;希望全国人民能关注我们，理解我们，支持我们，帮助我们&quot;。希望在网上把他说的这些话发出去，谢谢。<br />
