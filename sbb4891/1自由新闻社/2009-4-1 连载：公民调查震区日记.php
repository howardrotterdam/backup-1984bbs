													<h2>公民调查日记（连载七）</h2>
						
						
																											<div id="postmessage_70270" class="t_msgfont">公民调查日记（2009.04.06）<br />
 <br />
绵竹市有个板桥镇，板桥镇有个双河村。绵竹、板桥、双河，简洁漂亮的名字。<br />
在绵竹等车去板桥的车站，我们认识了小伙子贺涛，他来自板桥镇双河村。他说在绵竹打工。去年5月12日14点28分的时候，他就在这个车站站着，等待开往板桥的班车，他要回去看望刚怀孕的妻子。<br />
今天，他回去看望妻子和已经一岁的儿子。<br />
贺涛说，他的同事八岁女儿在地震中遇难，并给了我们同事的电话。<br />
车到板桥，我们要找的人在哪里？镇上人来人往，电话号码已经被没收。我们想起似乎哪位遇难女孩的家长似乎就在双河村，在贺涛的指点下，徒步2公里到双河。<br />
双河村分5个大队，散布在好大一片绿色麦田中。我们逐个询问，没有一位老乡记得起一位叫廖X的东汽中学女生。天空晴朗，可隔一会就会洒几滴雨。一条河流淌在这个村子中间，河的两岸怒放着鲜艳的绿草，走过河上的木桥，看见远处白色房子，深灰屋顶，黑色的电线杆。中午了，炊烟阵阵，恍如白日梦。<br />
我们坐在村子里的小卖部门前，一筹莫展。这时突然接到电话，那位家长的电话找到了。打过去，原来对方是在五星村，但人在板桥镇上。即约镇上见面。<br />
家长廖、安、孙热情的带我们到安X家中，他们的女儿、儿子都在东汽中学高中读书，地震后，只有孙19岁女儿的遗体被找到，安的18岁儿子和廖18岁的女儿都未找到。<br />
得知我们一天没吃饭，安执意安排做饭。在这次调查中，我们多次感到这样来自遇难学生家长的盛情。我们很担心我们所能做的事情愧对这样的盛情，但家长们说：你们是为我们孩子的事情来得，我们把你们当成亲人，即使你们什么都没有做到，我们也感谢你们。<br />
我们看到了238位东汽中学高中遇难学生的名单，上面有所有家长的签名、电话和摁的指印。“这份名单只有6份，是当时家长们聚在一起，逐个登记和确认的，我们约定，不管政府如何阻挠和骚扰，我们的电话都不能变”安说。“东汽中学高中共遇难300多人，但还有来自洛江、白隆等外地的孩子，没有能够联系到，至少还有几十位没有登记”。<br />
“这些孩子死的冤啊，早救几小时，最少多活100人。”想到自己的孩子，三条汉子一脸悲伤。<br />
家长孙用手机让我们看了他的女儿生前和逝去后的照片，我们很惊讶，两张照片除了后一张脸上有一点擦伤外，没有任何变化，一样的漂亮白净，笑容让大眼睛显得很亮。“她被找到时，完好无损，她不是被砸死的。找到时，大约刚死去几个小时。眼睛没闭。”<br />
“你们在东汽中学打听，我的女儿是没有缺点的。东汽厂厂庆，她是主持。小的时候家穷，但她上初中后，就全身的名牌，我想办法挣钱，就是让老婆和女儿吃好穿好。他女儿“是艺术类考生，地震前刚通过了四川美术学院的专业考试。”孙说。“后来生活好了，我想再有一年她就上大学了，她念大学的钱我也给攒够了，到时我也买个小车，不用很幸苦了。现在全没了，以前有人没钱花，现在有钱没人花。”。<br />
“我是这个镇上家长‘闹事’的头，我和太多高层见面对话过，我拍桌子骂他们是瓜娃子。镇政府每天都会给我打几个电话，没事就是问我人在那里，有个风吹草动，派出所就会找我，叫我打牌、洗脚。前几天，还有当官的和我说，北京有个艾未未，是美国和加拿大国籍，聘请了100多号人，投资几百万还是几千万来调查，你们绝对不要和他们下来的人接触，不要和任何记者之类的人接触。我说，你不能限制我的人身自由。”。说话的时候，果然接到政府打来的电话，“没事，就是问我在哪里。我的电话是被监听的，在哪里政府都知道。有一次，我在成都天府广场，那么多人，刚到，政府就打来电话，问你在天府广场干什么？为什么不去上访？去不了，我们出不去四川，我们连绵竹也出不去。”<br />
安的妻子说到最后也没有找到孩子的尸体，眼泪就流下来。她说：开始和我们说拿个头发就能鉴定出是不是我的儿子，我们家长都去抽了血，可后来再也没有消息，打电话问，说血都交到德阳刑警大队了，化验不出来。我们一些家长去问，他们说我们是闹事，关了整整一天，上厕所也跟着，没饭吃，不让出去买水。问警察说能不能给倒点水喝，警察说，你们闹事的，没有水喝。我们没有打，没有骂，没有偷，没有抢，我们闹什么事了？后来只要有家长们在一起超过20位，特警就来了，打人，女的也打，直接一脚就蹬上去了。<br />
“这是当时东汽中学的招生简章。”廖给我们看。上面写着：“在2006年，一所崭新的东汽中学将拔地而起。”廖说：东汽中学原来是东汽的子弟学校，后来面对社会招生，原教学楼建设于1975年，2006年前，东汽拨款数千万元给绵竹市政府用来建设新教学楼，直到地震也没动作，学生死了，我们问东汽，东汽说学校早已由地方接管。问政府，政府说东汽中学属于东汽。钱哪去了？责任谁来承担？只要我们问这个问题，就是闹事。“政府的人说，你们赶紧建设，孩子已经不在了，慢慢就忘了，淡化了。你说，我就一个女儿，养到18，我们现在40多了，我们能淡化得了吗？”。<br />
“我们知道人死不能复生，但我们咽不下这口气。我们没有反党。我们觉得党也不容易，就是下面的政府太不是东西了。”<br />
在安家儿子毕业同学录上，一个女孩子用秀气的字体写着：“桃花谢了，还有再开的时候，生命过去了，就再也不会回来了……”<br />
安妈妈说：这个本子里的孩子全死了。<br />
饭后，孙叫来一辆出租车，提出要送我们，我们说费用必须我们出，他想了想说行。<br />
上车后，他拿出手机，取出电池，抽走SIM卡，“这样，他们就找不到我了。”<br />
今天至此。</div>

							
							
							
							
