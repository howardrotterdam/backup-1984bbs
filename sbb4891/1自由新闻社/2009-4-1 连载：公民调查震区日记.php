													<h2>公民调查日记(连载五)</h2>
						
						
																											<div id="postmessage_69178" class="t_msgfont">(2009-04-06 00:22:09)<br />
<br />
时间：2009年3月31日晚<br />
<br />
地点：乔庄镇，青川县城所在地，乔庄派出所<br />
<br />
人物：乔庄镇派出所和县公安局刑警队<br />
<br />
3月31日下午，黄岳打电话给我，说在上网的时候接受派出所的盘查。问我是不是他们已经知道我们来调查名单了。早上接触的一个遇难家属给我电话说公安局的同志去了他家三趟，追问我们的信息，要我们快点离开青川。<br />
<br />
我接到黄的电话后，不能确认他们已经找到我们，于是和黄岳一起吃饭并且继续在网吧里整理收集到的名单。<br />
<br />
大约下午四点，网吧来了5个警察，直接找到了黄，要他出去说话。黄出去后，一名年轻的警察坐在黄的位置上，查看他上网的记录，同时查看了他QQ的聊天记录，这个年轻的警察（姓吴，80后，警校毕业生，工作一年）问我是不是一起来的，要求查看我的证件。他们带我们到网吧的门口，说这里说话不方便，要换个地方。我要求他们出示证件，其中一名警察说证件还在办理中，于是去守在门口。一名女警察出示了证件，并且告诉要带我们去派出所，因为我们违反了规定，没有办理长期上网卡（我们每个人交了三元钱，并且登记了身份证）。<br />
<br />
到了派出所之后，女警官直接让我上缴手机，并且翻看我的电话号码，我向他申明我的权利，问：你知道你在查看我个人的隐私信息吗？我要求保持我的权利，你带我到这里来的原因是上网没有办长期卡。她回答：我知道在干什么。我又问：我有权利保持我的个人隐私信息不受到侵犯。她回答：我没有侵犯你的权利，只是正常的查看。她拿笔抄下了其中的电话号码.并且交给了另外的一名警官。<br />
<br />
等待了两个小时后，我向女警官说：如果你事实清楚，我现在需要离开，你无权对我进行扣押。来的时候他们也带来了网吧的老板，两分钟后就开门出去单独和她说话，之后就放她回去了。女警官对我的回答：先聊聊吧，事实还不清楚。晚饭时间到了，我们在厨房吃了面条，之后我和黄被分开问询。<br />
<br />
问询过程：他们首先出示了证件，并且派了两名警官。没有给我出示权利义务告知书。态度还不错。询问至两个小时，其中一名警官要求我把身上的东西全部拿出来给他看，我告诉他如果只是在询问阶段，我有权保持我的权力要求平等的待遇，如果要对我进行搜查，请他们出示搜查证。<br />
<br />
他们的回答是：现在已经很晚了，如果要开个搜查证很容易的，只是人下班了。要我配合他们的工作，主动出示身上的物品。以确保我的人身安全。我再次申明我的权利，如果要搜查我身上的物品，一定要见到搜查证。于是其中的一名警官出去商议。<br />
<br />
在这个过程中，多次有其他的警官进来对我进行问话，其中一名中队长着便服，我求其出示证件，但他说没带在身上，我要求看到证件才能对我进行询问。于是他回去取，大约10分钟后拿到了证件，对我进行问话。整个过程中礼貌且态度温和。<br />
<br />
后来那个主要做询问的警察出去后，县公安局的刘局长进来，对我进行20分钟左右的思想工作，我经过申明我的权利后，还是掏出了我身上的摄像机和所有的物品，时间已经到了凌晨1：30左右。然后他们一行驱车带着我和黄到我们所住的旅馆进行搜查，并没有出示任何正式文件。一共有9名警官和四辆警车到了我们的住所，将所有的行李和房间进行了搜查，一一列单，带走了我们的调查信封，表格，笔记本。<br />
<br />
晚上的扣押单并未盖章，理由是太晚了人都下班，第二天才能给我们，并且要求我们第二天到县公安局报到。我们写完了再不能来青川做调查名单的保证书后，（保证书我写了5遍，理由是认识不深刻）在凌晨4：00左右送我们到了旅馆。询问时间12个小时。<br />
<br />
第二天上午8:30到县公安局国保办公室。找到了前一天晚上询问我们的警官，他们再一次带我们到了乔庄派出所进行问询，直到中午11：30 。<br />
<br />
这中间刘局长与我谈话，告诉我中午12点会送我回成都，要我有什么就说什么，不会为难我们。然后是一系列的手续，按指纹等等。完了后要取我们的被扣押物品，但是带着物品的警官因为昨天晚上问询我们时间过长，回去睡觉了。刘局长又带我们去取我们的扣押物品，找到了这名警官，在返还了我们物品的过程中，又对我们进行了教育。内容是这样调查名单会勾起遇难家属的伤痛，不利于灾后的重建。扣下了信封，表格和一盘录像带，并且撕取了笔记本里遇难学生名单和遇难家属的名单。我向他们申明权利，如果查扣这些物品，请给我出示公安机关的正式书面文件。回答是要我理解现在是非常时刻，而且送我们回成都的车已经到点，时间上已经来不急了。刘局长要求带有我们物品的警官快点，以免误了班车。送我们上车。到了车站，12点的车已经开走。于是又带我们回去县公安局吃了午饭。<br />
<br />
刘局长让我代他向艾先生问好，说灾后的重建工作压力很大。办案的警官们都很辛苦，希望我们建设完后再来青川。送上我们去成都的车。<br />
<br />
小于回忆</div>

							
							
							
							
