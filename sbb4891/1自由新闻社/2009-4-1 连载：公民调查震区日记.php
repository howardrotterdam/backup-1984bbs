													<h2>公民调查日记（连载六）</h2>
						
						
																											<div id="postmessage_69703" class="t_msgfont">公民调查日记（连载六）：被流产的遇难学生母亲 <br />
<br />
遇难学生徐聪家长徐兵与妻子赵管华，4月4日清明节欲前往聚源中学给孩子烧钱纸，在路上被公安殴打致伤住院，（周兴容在对话中提到的“另一位家长”）现在住在都江堰人民医院，医疗费用无人表态。<br />
之前2008年9月20日徐兵带妻子去都江堰检查身体，回来途中被截，分别关在奎光派出所和太平街派出所，24个小时。<br />
2008年8月5日徐兵被关在聚源镇政府一天，据徐兵说当时有王镇长，派出所所长药（？）志刚，邓警官，刑警陈大队。<br />
今年的大年三十晚被软禁在崇义镇一家茶楼内直至凌晨。<br />
<br />
遇难学生家长周兴容口述被当地公安局扣押并造成流产的经过。<br />
<br />
周兴容,都江堰崇义镇农民，孩子在地震中遇难.44岁，失业。<br />
<br />
周兴容：“我被他们当地派出所关了两次，而且导致我两次流产。第一次流产（就是）他们把我弄去拘留的当天在警车上就流了次产。是在7月19号，7月19号是关我一次，抓我去关了两天，逼我签了个申请；然后第二次抓我是9月18号。我那会就怀疑我怀孕了，晓得不，我就要求他们政府弄我去看病，去检查，结果他们没检查，说我去闹事，结果把我抓来，弄进去拘留了五天，当天在那个警车上就把娃娃给我整掉，可能3点过钟”<br />
志愿者：那他们晓得你流产了不？<br />
周兴容： “晓得，我告诉过他们，就是不要我出来，告他们，就把我弄起去拘留，就说的‘我给你教训，你不要去告我们，晓得不’在拘留所里头他们威胁我（那些）警官。都是2008年的事。”<br />
“然后出来过后，我那个月10月份，又怀起孕了，结果3个月，死胎在肚子里头。就是，就是他们嘛，他们关我那个月，五天。”<br />
“第一回关了我两天两夜，逼我，没得任何手续，把我骗起去强行把我抓起去，关了两天两夜，喊我签那个救助申请的。他们早就写好的申请叫我在高上签字。就是那个申请书，他们写好的，那个申请，就是写给娃娃的（遇难孩子）那个方案。我现在手头都有这个申请。”<br />
志愿者：他们还对你做过什么吗？<br />
周兴容：“就是，三十一又来撬过我的门，现在他们政府上给我换成新的罗。我旧门都还摆（在）这儿的，我还没有那个（扔）的，就是就是做证据。”<br />
志愿者：9月18号那5天他们又让你签啥子协议没有喃？<br />
周兴容：“那个没有，就是我就说要上诉他们晓得不嘛。嗯，第一个要上诉他们，嗯上诉他们就是告他们上次关我告他们；嗯然后第二个原因我就是去找他们医病，晓得不嘛，去检查。”<br />
志愿者：那他们现在是啥子态度喃？是愿意给你解决？<br />
周兴容：“他们现在我现在等于是说莫得告他们的那个噻，他们（现在）告不翻他们，晓得不，告不翻他们，晓得不。”“走不出去，我们现在说白了。我们清明那天去烧纸，还没走拢学校，当街就被抓走罗。哦，就是4号那天嘛。噢~七八个警察，按上（来），便衣警察。在街上，当到那么（多）老百姓，按起走嘞……还有我们这儿还有位家长，现在还在医院头，还伤都没整（好），他们片（检查的X光片）都不拿，现在连病都没看到，今天来还在这儿等。”<br />
志愿者：他们被打啦？<br />
周兴容：“哦，就是。”<br />
志愿者：呃，一共有好多个家长那天？<br />
志愿者：那，那个你说你门被撬是好久的事？<br />
周兴容：“门是三十一，就是25号那天。”“啊~我们没得办法得晓得不嘛，莫得辙又告不到他们晓得不嘛。”<br />
志愿者：请问一下你的工作是啥子喃？<br />
周兴容：“我们，我们两个都失业，是农民得嘛，没得职业，啊，现在我们两个还住在帐篷里头晓得不。莫得板房就住的帐篷。我的房子宾（皲）了，不敢住，晓得不。”<br />
志愿者：那他们撬你的门也是帐篷的门？<br />
周兴容：“不是得，是笼闷子（围墙的大门）。就是那个都江堰市我报了案过后，第二天我丢了钱过后他们才来给我还拍些照片这些下来。”<br />
志愿者：那那个钱是不是他们拿的呢？<br />
周兴容：“钱不晓得是哪个拿得。嗯，我是初二掉的钱，一万八千块钱，哦，晚上掉嘞，在帐篷里头。”<br />
志愿者：帐蓬头？那你们现在主要是住帐篷还是屋头喃？<br />
周兴容：“帐篷，现在都还是住帐篷。”<br />
志愿者：是不是现在因为屋头是危房了不能住喃？<br />
周兴容：“哦，就是危（房），就是房子烂了不敢住，才怕又摇，地震。”<br />
志愿者：那你现在好大岁数喃？<br />
周兴容：“四十四罗就是。哦~ 对我打击，打击太大罗，哦~”<br />
“你们是哪个地方喃？”<br />
志愿者：我们是北京的一个志愿者小组。<br />
周兴容：“哦，是不是北京哦？”<br />
志愿者：是北京，嗯，我是四川人在这儿当志愿者。<br />
周兴容：“哦。”<br />
志愿者：嗯，因为这边只有我会说四川话。<br />
周兴容：“哦哦哦……好好好”<br />
“哦，那就这样子。我们的电话被监控罗。……”<br />
