																											<div id="postmessage_70718" class="t_msgfont">何洪春事件的陈述与看法 09.04.08 <br />
<br />
<br />
4月3日，北川二组在绵阳与几位北川遇难学生家长（以下简称学生家长）见面。学生家长谈到与一位叫何红春的相关事情（以下简称何红春事件），希望我们能提供帮助。<br />
<br />
何红春事件介绍（以下内容均来自当时谈话）<br />
<br />
 <br />
<br />
事件起因： <br />
<br />
北川地震后，安县保险公司开始对遇难学生进行理赔。学生家长们发现了诸多问题：<br />
<br />
1、学生们生前购买的保险所缴纳的费用，均有学校统一扣除，属于团体险，而且保险合同上无监护人签名。根据法律这些保险合同是无效的（2005年政协关于保险条例的修改条例的规定，人寿保险凡是涉及死亡等赔偿的相关内容，保险合同必须由具有法律资格的人签字方可生效）；<br />
<br />
2、保险费交纳为30元，但保险公司只受到8元钱，有22元/人被用于其他地方。学生家长认为这里存在违规操作。<br />
<br />
学生家长因此决定与保险公司理论并打官司。最先，政府同意学生家长们选出代表与保险公司交涉，之后政府又不允许家长的行为，故学生家长开始找政府理论，并在交涉过程中发生了“堵门”等事件。（关于北川家长与保险公司之间的纷争等相关事情，以下简称保险门）<br />
<br />
 <br />
<br />
事件经过： <br />
<br />
何洪春的侄女是此次理赔事件的当事人之一，在保险门事件中，何红春被政府选为与保险公司的交涉代表之一，并参与了此后所发生的事情。在去年9月22日，何洪春被政府以“聚众扰乱社会秩序罪”等十项罪名拘留，原定于2009年4月3日审判，后因故推迟到2009年4月14日开庭。<br />
<br />
 <br />
<br />
事件结果： <br />
<br />
学生家长在何洪春被拘留后，曾联名写了证明信证明政府的指控不成立。并要求政府在开庭审理何洪春时同意学生家长们旁听。<br />
<br />
家长们找到我们，是希望能将此事件发布到网上，引起国内网民关注，从而借助舆论的力量影响何红春事件的走向。<br />
<br />
&nbsp;&nbsp;<br />
<br />
个人看法： <br />
<br />
由于本人对此次事件并无深入了解，又无法做到对当事双方进行深入调查，同时对相关法律知识了解不深，故无法给出客观评价。只能根据此次谈话的内容，写出以上文字。<br />
<br />
关于此事件的详细内容，学生家长未能提供，只是口述了一些内容。我曾在交流中提到他们可否提供相关内容，但均无回应。个人判断可能是担心被政府报复。<br />
<br />
古人云：“路见不平，拔刀相助”。由于二组此行北川非为何洪春事件而来，且我们都没刀，只能将了解的内容成信一封，发于大家。<br />
<br />
