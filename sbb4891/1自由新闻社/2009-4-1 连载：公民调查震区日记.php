													<h2>天朝裂缝数日游</h2>
						
						
																											<div id="postmessage_67657" class="t_msgfont">艾未未博客 公民调查日记<br />
<br />
&quot;非那条法，到时候我们会告诉你们&quot;<br />
<br />
1、<br />
<br />
2009年4月1日，近11点，我、李X和徐X在什邡市矿业招待所退房后，在对面的街边吃过早饭。<br />
<br />
我们找了公用电话，想联系一个遇难学生的家属，拨通号码，刚讲了两句，就看到徐X傍边凑近几个人，一个40岁左右的男人过来问，他说你们是不是搞学生调查的，徐X回答是，他说他是公安局的，让我们跟他走一趟，跟他一起的还有一个年轻的警员。当时李X进了附近的药店买药，来的人好像并不知道我们一行是三个人，我回头看了一眼李X，和徐X一起跟他们上了一辆非机关牌照的银色捷达车。上车后请他们出示证件，年轻的男子出示了他的警员证，名叫张远觅。他们说请我们去谈谈话，车开到了不远处的什邡市城西派出所。我发短讯给李X说“阴天”。<br />
<br />
 <br />
<br />
进去之后，我问那个40多岁的警官叫什么名字，他说他姓叶。他们态度比较好，几次让我们喝水，还给徐X发了烟，叫我们坐在沙发上，身份证件拿给他看，并登记。<br />
<br />
进来一位王队长，一身西服，打着领带，态度也还好。他们问我们来什邡干什么。我们说来看看灾后的学生。我问你们是怎么找到我们的，为什么要带我们来？叶队长笑说：哎呀你们这话就不对了，怎么能是带呢？这是请么。我们看到像你们这样背包的客人来了什邡，怕你们不安全，把你们请来看看需不需要有什么帮助。你们来我们什邡我们是非常的欢迎的。就这样随意的聊了一会。我问他们什么时候能让我们走，他们终是回答说，再坐一会嘛，喝点水。<br />
<br />
他们进进出出，打电话，在商量事情。很长时间过去了，我问了王队长好几遍还有什么问题，我们需要离开，他们都说坐坐嘛，等一下，一会请你们吃个饭。我说我不要坐，更不想吃饭。这时，我想到他们登记了我们的证件，而我们在德阳的新号码是用徐X的证件买的，就给李X打了一个电话，说你把你德阳的号码关机，问我们的话就说是丢了，尽可能不要再扯入第三个人。<br />
<br />
 <br />
<br />
持续到13点，王队长进来说你们俩来录个口供，两个人要分开询问。<br />
<br />
后来听徐X说他被带到先前的那个接待办公室，王队长开始教育他，语气比开始重了，徐X说你是不是威胁我，他说他是气愤，说徐X不学好，来给灾区添麻烦。这个时候进来了个年轻警官，叶警官把他叫到外面交代了几句，然后王队长让这个年轻的警官给徐做口供。记录了徐的身份证，然后问他的调查目的是什么，谁组织的，有没有经费，我们除了调查学生情况还调查了什么。因为怕牵扯他人，关系到学生的信息徐都尽可能回避了。<br />
<br />
警方问徐跟我怎么认识的，问得很细。徐说是在左小祖咒签唱会上认识的，他问左小祖咒怎么写，徐给他写，“咒”忘了怎么写，他问是不是“诅咒”的那个“咒”，徐说是，他就自己写上了。后来这个办公室带了两个打架的人进来，他们是三轮车车夫和乘客，其它的警察在询问他们情况。<br />
<br />
我被问话的地点是在值班4室。给我录口供的是带我们来这里的警员张远觅。他先登记了我的基本信息，之后开始问话。<br />
<br />
我打断了一下，我说：请问你们是有权利这样在大街上带人回来就直接开始问话的么？有没有法律规定是可以的？<br />
<br />
他说：有的，任何公民。<br />
<br />
我说你问吧。他问我你们几个人来的？答：两个人。问住在哪里？答：路边找的招待所。问来干什么？答：看看灾区的现状，纪念下遇难的学生。问你们去过哪些地方？答：洛水，红白，蓥华。<br />
<br />
这时候一个电话打来，他跟对方说知道了，明白。继续问。这时王队长推门进来说：你们有个博客是怎么回事？那个博客是谁的？你们不要以为我们在街上是随便把你们带回来的。这个时侯我觉得这件事情他们是了解的，是有人看了博客才说出来的话。<br />
<br />
我说是艾未未的博客。<br />
<br />
问是他让你们来的么？答：我自愿来的。<br />
<br />
问谁给你们出的经费，是艾未未给你们出的钱么？<br />
<br />
答：我们钱不多，给我们出的车费。<br />
<br />
问：那就是说是他让你们来的？<br />
<br />
答：你这个人说话怎么老想把别人给带进去呢？没有什么人命令我们来。<br />
<br />
问：你们来的主要目的是什么？<br />
<br />
答：收集学生名单。<br />
<br />
问：名单拿来干什么？<br />
<br />
答：纪念遇难的学生。<br />
<br />
问：怎么纪念？<br />
<br />
答：现在只是尽可能收集到这些名单，公布出来。<br />
<br />
问：这个死亡数字不是都公布的么？<br />
<br />
答：我们需要具体名单。<br />
<br />
问：你们都是怎么问名单的？<br />
<br />
答：墓地里抄来的。<br />
<br />
问：你们见过多少家属，都说什么？<br />
<br />
答：很少，在墓地遇见过学生和家长。就问你的朋友或是孩子的名字。<br />
<br />
问：你觉得问这个到底有什么用？答：我觉得这是唯一可以纪念他们的东西了，就是想知道他们的名字。我在网上看到一个母亲说过，她只想让全世界都知道她美丽的女儿曾在这个世界上快活的生活了7年。说到这里我哭了。<br />
<br />
张警员没再说什么。问我还有什么要说的。签字画押。我比徐X提早问完约40分钟，天气特别的阴冷，坐在沙发上，盯着那扇进进出出永远不会被好好关上的门瑟瑟发抖。张警员拿进来三个盒饭，本不想吃，但身上很冷，吃了两口却也实在是难以下咽。<br />
<br />
徐X后来更没有心情吃。<br />
<br />
 <br />
<br />
等待徐X的时候，叶队长从外面带来一个女的，挺好看的。见到我就很热情，让我喊她谭姐。我问她是做什么的，她说是做政府部门接待的，她的领导听说派出所带来的人，就叫她过来看看。<br />
<br />
她跟我聊了一会儿，说自己也是孩子的妈妈，地震的时候的事情也留给她很多感触。但不管怎么说，她对我们大老远从北京跑来做这个事很不理解。<br />
<br />
终于等到徐X出来，又来了个曾处长，西服鞋子特别干净，领带打得很好。他给我们介绍了很多什邡灾后政府出台的民生政策，说了很长时间，有包括建房补贴，生育政策，独生子女教育等等。最后停了一下，看着我说，你们年纪还小，应该好好工作，这才是对我们灾区最大的贡献，现在社会很复杂，你们不要被一些非法分子或是国外敌对势力所利用了，我们不是不相信你，不是你们说是就是，说不是就不是的。<br />
<br />
 <br />
<br />
曾处长出门打电话，过了将近20分钟的时间，谭姐进来说我们可以走了，上级让我们最好离开什邡，说我们对这里不熟悉，他们可以送我们去车站，现在就坐车去成都。接着她又出去接了个电话，回来说我们能不能写个保证就说以后不来什邡调查了。我听着很别扭，我说这个我不会写的，我没做什么坏事，也不需要写什么保证书，于是谭姐又出门拨了一通电话，说我们可以走了。这是是17点20分。这是我跟徐X第一次进派出所录口供，有点紧张，天又特别的冷，徐X一直都有点发抖，不过他还能控制。<br />
<br />
 <br />
<br />
后来谭姐、孙警员和一个司机用一辆墨绿色奥拓把我们送到什邡南门车站，孙警员送我们下车，我说你们先走吧，我们想买点吃的，他一直看着我们，直到看着我们买好两张什邡到成都的车票，等我们上了车，孙警员还沿着车窗确认我们是否在车上，我和徐X坐在最后一排，我看他在下面找的太累，就伸手敲了敲车窗，他这才看到了我，我冲他挥挥手。车开了，他们也上了绿色小奥拓，并开车跟着我们走了很远，直到开到一条通往成都的分叉路口时才离开了我们的视线。<br />
<br />
 <br />
<br />
于是我们在车上赶紧与李X联系，在车上借了一个邻座乘客的电话，打了一通给李X，说你打车回家吧，早点回家吃饭。这个时候李X应该是去超市去包，然后打车回成都跟我们见面。路上，我打电话给财经记者李X，他上午给我打过电话，那时候正在问话，我就没接，我跟李X说我们在回成都的路上，他说我把地址发给你，晚上可以住我这里，等你们吃饭。<br />
<br />
我和徐X 19点30分的时候回到成都车站，出来用公话打给李X确认他也在回来的路上。但李X说他坐车回来的路上看到司机接了一个电话，并回头看了他两眼，对着电话里面说：红衣服。<br />
<br />
这让我们很郁闷，我和徐X迅速打车，路上和李X通话说在人民公园见面。<br />
<br />
车开到人民公园，我掏钱结账，这时李X打开前门看见我，打了声招呼，跟师傅说在往前开一点吧，就在他要上车的时候，一个人把他从车里拉出来，探进头问徐X叫什么，要看我们的证件。我们要求他出示了证件后，下了车，才看清他们有十多个人，都是便衣，阵势很惊人。<br />
<br />
我和徐X被带到一辆车后排，我坐在中间，右边一个警察在开车后用他的伞把将徐X那边的门锁按了下去。应该是习惯性动作，怕我们伺机开门跳车吧。<br />
<br />
 <br />
<br />
成都下着小雨，警车开去青羊公安局汪家拐派出所。路上，开车的人态度很凶，对我们说你们要是不老实配合，就叫你知道我们的厉害。然后就命令我们交出手机，交了一个之后还问有没有了。我听不懂太多的四川话，后来徐X说，一个警察在车上问青羊公安局的警察说你们那儿有没有手铐、脚镣什么的。对方回答说局里有吧。到了派出所，我们被分开办公室问话。<br />
<br />
 <br />
<br />
徐X那边身上、包里的东西全部翻出来，然后让他等着。一个年轻的警员守着他，后来进来一个头发花白的警官，他准备好问话材料，和年轻警官互相对了一下眼色，点了个头。然后开始问话。<br />
<br />
一开始就是说徐X如果不说实话要负法律责任。徐X把所有的情况都如实回答了一遍。后来问得差不多的时候进来一个戴眼镜的老警察，继续问他，比上一个凶一些，吓唬他那种感觉，老是问做调查是不是有别的目的。徐说没有，重复了很多次。后来问完了，就坐着等。<br />
<br />
由于没有什么好隐瞒的，所以徐说不紧张，就是很长时间没吃饭。因为门开着，可以听到隔壁警察在说话，感觉他们比较高兴。然后还说徐比较好审问，有人还夸奖那个领导说刚开始的工作做的好。徐觉得大概意思是说给我们的下马威好吧。后来还听到有人说艾未未的博客就让他开着嘛，只要他再写得过火点，就直接给他关了。大概是这个意思。他们都说四川话的。<br />
<br />
 <br />
<br />
我那边问话的是编号007024的吴鸣春警员，是一个比较年轻的刑警，他在一遍登记我基本信息的同时，刚刚那个开车带我们来的警官进来，要我把包里面的东西全部拿出来。我包里面除了一个记事用的本子和相机之外就都是一些日常用品。他把我的钱，发票收据，公民调查的证件翻来覆去的看，最后先是把钱放进我的钱包，叫我放好，其他都排在桌子上面。这时我的手机一直再响，他们不让我接电话，我知道一定是李X打来的，特别着急，有点慌。我和徐的手机都放在我的桌子上面，两个手机一会这个响一会那个响的，闹的我心里很难受。那人看到李X的来电问我李X是谁？我说我们一起来的朋友，现在成都。他说你给他打个电话，别告诉他你在公安局，问他在哪里就可以了。我说我不告诉他我在哪里他也会知道的，我就直接说我在这里好了，你们去接他就是。他不同意，我给李X打电话，李X说你在哪？我说你呢？他说你是不是说话不方便？我说是，你在哪？他说就在人民广场，肯德基门口。我说那你在门口等吧。他说你是不是在公安局？我说是。于是他们就出门找李X了。<br />
<br />
其实肯德基就在公安局门口，后来李X还说，你们怎么约了离公安局这么近的地方见面?<br />
<br />
 <br />
<br />
白天在什邡冻了一天，晚上又被成都的便衣带来，顿时感觉身心疲惫，一直在咳嗽，刑警们看我这样，就主动给我倒水，态度算是可以。笔录和白天的差不多，只是更加详尽了一些。一听就知道他们对我们的行踪已经掌握的相当准确。所以回答问题的态度也特别放松，毕竟是没有什么需要隐瞒的。<br />
<br />
在后来问话的过程中，我看到李X包里的资料也拿到我的桌子上面，知道李X也到了这里。对我们三个人的问话一直持续到了23点，我们在什邡搜集到的材料，全部被扣押下来了，里面有我们的名单、日记、相机卡，有家长们和幸存下来的孩子们亲笔写的东西……。<br />
<br />
警方没有给我们开具扣押物品的清单。《中华人民共和国刑事诉讼法》第一百一十五条规定：第一百一十五条对于扣押的物品和文件，应当会同在场见证人和被扣押物品持有人查点清楚，当场开列清单一式二份，由侦查人员、见证人和持有人签名或者盖章，一份交给持有人，另一份附卷备查。<br />
<br />
 <br />
<br />
最后一个警官把我们叫进一个房间，我们三个人终于在时隔12个小时后见了面。又坐等了大概20分钟，一个警官说我们可以走了，让我们会酒店好好休息，不要有什么顾虑。说明天市局的人要来问我们情况，总之有事没事都会通知我们的。<br />
<br />
 <br />
<br />
2、<br />
<br />
 <br />
<br />
下午18：30左右，在网吧终于接到赵X电话，语音低沉：我们已出来，现在开往成都客车上，是由警察直接送到车站的，且后面还一直有人跟着，你打车来成都吧。<br />
<br />
我遂结账，考虑到正式出租车现在驶往成都也许更容易被注意，就打听是否有其它选择，网管说，烟厂东门，“黑的”很多，驶往成都，约需1个半小时左右，150元上下。<br />
<br />
我步行到所指地点，果见十数面的。租其一先驶往存包地，取出背包，坐后排，驶往成都。<br />
<br />
刚至什邡界，司机接电话，似有人问拉什么人和去哪里，司机扭后瞟望，道：红衣服。<br />
<br />
我即开始注意司机是否有停下或拉我返回什邡之意，意料之外，约80分钟左右，即到成都。<br />
<br />
途中与赵X短信联络，知其已至，约人民公园见。<br />
<br />
成都火车北站下车，即换出租车至人民公园，其时天小雨，渐至城市中心，霓虹烁地。<br />
<br />
人民公园下车，见肯德基，天冷肚饿，入内买热饮，即电赵X2遍，无人接听。心知不妙，彷徨无计，盘算先住下，安置背包，再做打算。出门见如家快捷，即订房一间，将包存在前台，入房间。此时赵X来电，问我在哪里，没有告知，问之：是和公安在一起吗？答：是，公安已全部了解情况，你在哪里，我们过去找你。<br />
<br />
我想先离开酒店，刚出店门，即有一50左右男子尾随，叫我名字，旋即另两位中年男子过来，说：公安局的。我要求出示证件，对方较年轻者拿出警官证，但天黑之中，未看清号、名。<br />
<br />
三位将我拥至酒店内，问我房间号，并迅速拽过房卡，问询我随身携带行李，我说：不用上去了，就在前台。其中两人上去搜查，另一人与我在酒店大堂，取出背包。5分钟后，两人下来，带我去派出所。我才发现，肯德基、如家都在派出所隔壁。<br />
<br />
到派出所被安排到2楼一办公室，一中年便衣男子开始问话，同时带我来的警察要求我将身上所有东西放到桌上，并迅速开包检查，将包内物品逐一取出、查验、分类放于桌上，后将<br />
<br />
手机取走，不知拿到什么地方。（后临走时归还）<br />
<br />
一直到23：15许，分别由三位警察轮番问话，问题大致相同：你是谁？你从哪里来？你到过那里去？你为什么来？要做什么？做了什么？怎么做的？为什么做？谁让你做？让你怎么做？给你提供了什么（钱？物？其它？）？下一步准备到那里去？我态度坦诚的一一回答：我的简历；从北京来；到过成都、什邡、蓥华、洛水、红白；为在地震一周年之前对遇难的学生做纪念，有名有姓的纪念；调查、统计遇难学生的名单；已统计到100位左右；通过和家长、学生的了解进行统计；就为了让自己和全国关心遇难学生的人们具体知道谁遇难了；我们志愿者自发进行活动；没有人教，因为没有人知道怎么做和会遇到什么情况；志愿者团体自费，各小组有交通食宿的基本费用补贴；下一步还将继续这些工作。<br />
<br />
我大致如此真实、真诚、真切的回答了三位警官分别提出的同样问题。最后在笔录上签字，并用右手拇指摁下指纹。<br />
<br />
问话其间，有2位年轻人进来，分别持数码相机和摄像机，让我站到墙边，拍照数张，并将桌上我包中资料等悉数拍照、摄像。<br />
<br />
三人问毕，均出门，一会一年轻警官进来，指示我将东西装好，下楼听取安排。（因丝毫无此方面经验，后来得知，依据法律，警方扣押物品均需出具清单，并签字确认；另外问话时警方不得少于2人并均需出具身份证件。可见不是人家不尊重我的权利，只怪我都不知道自己有何权利。）<br />
<br />
下楼后终于见到赵X、徐X两位。时已凌晨。有警官告知：等着，一会给你们安排和答复，但几十分钟过去未见人来，我们也不好意思去问。<br />
<br />
终于有一警官过来说：1、你们可以走了。2、明天不要离开，市局的人会找你们对话。3、没事，好好休息吧。我们问：明天大概什么时间对话。对方答：9、10点钟吧。<br />
<br />
今天至此。<br />
<br />
 <br />
<br />
2009.4.2<br />
<br />
 <br />
<br />
早九点起床，在酒店房间等待通知去对话。<br />
<br />
但直到11：00，没有人通知我们，酒店需要退房了。我们考虑是继续等通知呢，还是主动一点去派出所找对方，最后决定先退房，去外边吃点饭，昨天一天大家都未吃饭，等通知。<br />
<br />
下到酒店大堂，我正办理退房手续，门外一位警官进来，到前台站我旁边问服务员：哪个XX退房了没有。我慌忙说：我就是，我就是。警官说：我来看看你们退房了没有，然后看看你们是否需要什么帮助，比方说送你们去火车站什么的。我说不用了，我们还要等市局领导通知我们对话呢。我们要求这位警官出示证件，警官迅速拿出。警官说：不用了，今天不对话了。你们准备去哪里？我们说：现在还没定下来，我们还得找昨天的警官，把我们被扣押的东西开个清单，其中还有一些我们个人的东西，想要回来。警官说，哦，这样的话，你们等一下，即出门打电话，一会另外一位警官进来，被介绍是青羊派出所的刘所长，在我们要求下，也出示了证件。&nbsp; &nbsp;&nbsp; &nbsp;。<br />
<br />
我们将意图复述一遍，刘所长说，哦，这样的话，你们等一下，即出门打电话。一会进来说，我请示过，大概有这两个意思：1、你们的物品已被送往有关部门鉴定，待鉴定结果出来后，才有可能处理，或者归还等；2、你们不要再从事非法调查活动，劝你们尽快离开成都，越快越好。<br />
<br />
我们问：1、鉴定需要多长时间？现在请先给我们一个物品清单；2、我们的调查为什么非法？非那条法？为什么必须尽快离开成都？如果我们在成都玩几天是否可以？如果我们继续统计遇难学生名单会如何？<br />
<br />
就这2个核心问题，我们讨论了大约1个小时左右。<br />
<br />
关于第1个问题，刘所长开始说鉴定也就几天时间，我们说，那好我们就呆几天等到答复再走，刘所长又说，你们还是先离开成都，这个鉴定短期内不会有结果。<br />
<br />
关于第2个问题，刘所长表示，调查肯定是非法的，你们认为不非法可不见得就不非法，至于非那条法，到时候我们会告诉你们。我只是劝你们离开成都，在这里待着对你们没有好处，只有坏处，至于是什么坏处，到时就知道了。<br />
<br />
最后，我们提出要去吃饭了。刘所长再次郑重表达了劝我们离开的意思。我们即离开酒店。<br />
<br />
下午15点，我们给刘所长去电，重申了想尽快得到被扣押物品清单和个人物品的意愿，刘所长仍答：等鉴定出来后即会给你们答复，但这个鉴定短期内不会出来，劝你们尽快离开成都。<br />
<br />
今天至此。</div>

							
							
							
							
