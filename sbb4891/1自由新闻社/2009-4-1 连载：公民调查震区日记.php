													<h2>我只是不愿意再被嘲笑罢了</h2>
						
						
																											<div id="postmessage_69176" class="t_msgfont">(2009-04-05 12:47:45)<br />
<br />
我只是不愿意再被嘲笑罢了<br />
<br />
离 “5.12”周年纪念日还有60天的时候，艺术家艾未未在自己的博客上公布了68份名单，这里面有1579名地震遇难学生的姓名、年龄、学校、班级，接下来的几天，他又开始陆续上传“遇难学生名单补充”，期间一些名单被删除，他写下一句：“是什么人，为什么缺德呢？”重新上传。<br />
<br />
暂时不再有删贴，截至3月22日，统计出来“有名有姓”的遇难学生总数是 1936人，他希望在一周年到来之前让这个民间发布的名单尽量完整，“我们可以回避这些血和肉，这些声音，这些气味吗？”<br />
<br />
“地震一下子把我打懵了，大概有10天时间，不知道该说什么好，”十几天以后，艾未未去了四川，到了除北川几乎所有重灾区，“回来以后也不知道该做什么好，这个事情太大了。”<br />
<br />
2008年年终，他开始准备2009年年底在慕尼黑的一个展览。“我想做一个跟死亡有关的作品”，这时他想起了在灾区看到的书包文具，“遍地都是，我想知道，是谁用了这些书包，他们叫什么名字。”<br />
<br />
但官方并无遇难学生的名单统计，“按照我们自己的判断，应该是民政部门负责这件事，”艾未未工作室的刘耀华说。于是第一通询问电话拨往四川省民政厅，“感觉他们并不知道是谁负责，让我们去问公安厅，公安厅又说归民政厅……”<br />
<br />
从省、市依次往下，电话咨询无不令人失望。“其实我们希望他们说，好，你们不用来了，这个事情交给我们吧，”艾未未说，“在过去的300多天里，他们一直有这个机会，但是他们没有用。”<br />
<br />
到3月13日，艾未未觉得有必要重新拨打这些电话，并一一录音，“我们希望别人知道我们是做过这些事情的。否则有人会说，你为什么给政府出难题啊？为什么不通过政府来做啊？”<br />
<br />
在这一天里，艾未未和他的同事总共打了150个电话，他们把部分电话录音整理出来，挂在博客上。一个拨往某重灾县维稳小组组长的电话被记录如下：<br />
<br />
“我们刚才的电话还没讲完你就挂掉了。”<br />
<br />
“你这么关心这个事情呀？你们有什么目的呀？”<br />
<br />
“我们没有什么目的呀。”<br />
<br />
“没有目的干嘛关心呀？”<br />
<br />
“我们为什么不可以关心，这是中国人的事情呀。”<br />
<br />
“我也是中国人呀！你要是美国人派来的特务呢？你要是美国人派来的间谍，怎么办？”<br />
<br />
……<br />
<br />
“既然我们政府部门已经公布了，那就可以了，你还要问，我就怀疑了，我要维护国家利益呀！”<br />
<br />
“我们都在维护国家利益呀！但是国家也需要维护人民的利益呀。”<br />
<br />
“是，那就是政府的事了，你不需要管这个事。”<br />
<br />
“我们是公民呀，我们想要求你们负起责任来呀！”<br />
<br />
“你怎么知道我们没有负责？你凭什么这样说？你有必要把话说的这么难听吗？”<br />
<br />
“这不是难听不难听的事，这是事实呀！”<br />
<br />
“你说是事实？我直接就怀疑你就是美国方面派来的女特务！”<br />
<br />
“国家秘密”、“国家纪律”和“个人隐私”是拒绝提供名单的三大理由，也有较为“老实”的回答：“我们没有权力向外提供”，或者，“这涉及到社会的稳定”。150个电话，只有一位学校的校长真正回答了问题，他们学校有两位学生在地震中遇难。<br />
<br />
有网友在这些问与答下面跟贴，说怎么读着这么像荒诞小说？艾未未说，“正是这很多问题，我们叫做文化，构成了中国社会的现状。”<br />
<br />
被怀疑为“美国女特务”的赵颖是艾未未的同事，在拨打电话中，她最常被问及的问题是“你是什么单位？你想干嘛？”<br />
<br />
“当我们回答是个人时，有人就在电话那边冷笑，”这个年轻的女孩子睁大眼睛，强调了一遍：他们在冷笑！这还不是最叫人吃惊的，在另一通电话里，她问对方：他们（遇难学生）是我们的同胞呀！对方反问：是呀，是我们的同胞，关你们什么事？<br />
<br />
当然，多数回答者并不这般“理直气壮”，他们乐于提供其他部门、小组的电话，“基本上，就在民政、公安、教育这三大部门兜圈子，”赵颖说。一个非典型皮球的传递过程如下：宣传部――救济救灾科――基教科――德育科――维稳组――宣传部。<br />
<br />
而一位接到过询问电话的当地工作人员对《纽约时报》记者说：“（艾未未或者他的同事）简直是个疯子，不停地一遍又一遍地问问题……”<br />
<br />
遇难学生名单统计是一项庞大的工程，起点是谷歌、百度提供的无数链接，和这些链接里面的更多链接。<br />
<br />
刘耀华和赵颖笑称自己的工作是“全手工”：搜索类似“汶川地震遇难学生”之类的关键词，然后挨个点开所有的文章，记录下相关的名字和资料。就这样他们搜集到了700多个遇难学生的名字，“只能靠眼睛，所以尽管我们筛查了两遍，还是不断地发现错误，包括重复的，名字写错的。”刘耀华说。<br />
<br />
好在他们并不孤单。在公布的遇难学生名单中，有338位附有家长的电话或者手机号码，一个高中男孩的母亲在3月15日看到艾未未博客后，每天给这些手机号码发送问候短信，“我是一个没有用没有本事的人，这样做是太心疼这些孩子了，”她给艾未未写电子邮件，告知他们短信回馈的结果，纠正他们弄错的名字。<br />
<br />
更多的人发信询问“我可以做些什么”并留下自己的电话，有建筑师、有家庭主妇、有商人、有即将毕业的大四学生，还有一位人在通州的“80后农民工”，他们中的很多是人在外地的四川人。<br />
<br />
赵颖准备分头联系他们，工作室按照新闻报道和网上的种种说法，筛选出四川省受灾最严重的79所学校，如果不出意外，工作室招募的志愿调查员将在近日出发。<br />
<br />
在确定学校名单的过程中还有一小段插曲。赵颖给四川省教育厅打电话，希望对方能提供四川省境内所有学校的名单，得到的回答是“网上有”，于是他们登录教育厅官网，查询到389所学校的名字，“四川的大中小学总共只有389所？你信吗？”刘耀华说。<br />
<br />
“长期以来，那些年长的人告诉我们，不要管这个事情，改不了的，”艾未未说，“但现在已经开始变化了，有人会说，让我们做一点什么。网络为大家提供了一个不可想象的技术手段，这是没有办法挡住的，为什么我们不用新的方式来看待世界呢？”<br />
<br />
在艾未未看来，公开和透明是实现社会公平正义的最基本手段，“不然，整个社会就是一个豆腐渣工程”。基于这样的认知，他决定招募大批志愿者来参与这个调查。<br />
<br />
“ 因为一个人参与很容易被威胁、被限制住，我们会有50个人，他们限制的话，我们会有200个人、会有1000个人，这时候他们会想一想，你这样限制是否有可能……如果他们够聪明，他们不会把一个很小的调查变成13亿的人的调查，但是，看吧，看看他们有多聪明，我没法高估他们的智商。”<br />
<br />
事实上，艾未未工作室的两位同事，已于春节前先行出发，在四川灾区走访了一个半月，带回来超过800名遇难学生的资料和80多个小时的录像带。<br />
<br />
抚摸一个被认为已经“痊愈”的地方是困难的，有的家长不愿再提过去的痛苦，有人则两手一摊：有什么用呢？记者来过了好几拨，也没有看到报道，还有些家长则是面临种种现实的压力，不敢受访。<br />
<br />
受灾城市的民情也有不同，在成都以北的一个城市里，他们说服了一位家长接受“不现身”的采访。然而在对话中，镜头还是不小心扫过了他的衣角，在回看录像时，这位家长坚决地要求他们删掉这一段，“他们会从衣服认出我来的！”<br />
<br />
在另一个城市，二十几个遇难学生的家长（他们中的大多数是妇女）在拘捕令上签了字后得以回家，在此之前，她们因为聚在一起研究学校建筑的质量问题而被人认为“煽动……”，“她们都是30多岁的女的，手无缚鸡之力，请问她们能闹什么事呢？”一位工作室的同事说。<br />
<br />
在一些地方，他们被好心的家长警告：小心一点，什么情况都可能发生――不论你是外国记者，还是央视记者。他曾经因为场记单的险些遗失而吓出一身冷汗，等找到时，场记单已被人动过，“那以后我们的场记单就只用一张纸，即便没有了，也只不会丢失所有的学生名单。”<br />
<br />
比害怕更普遍的情绪是认命。他们用的是“笨办法”，“我们不选取家庭，只要有遇难学生的，就通过家长间的介绍一路访问下去”，而访问之中，他每每惊叹于被采访者的忍耐，这让他感到格外的悲凉。<br />
<br />
“为什么我们要寻找学生？因为他们遇难得最为集中，因为他们教学楼旁边的房子没倒，”他说，“你想想，这是整整一代人啊，怎可以一笔带过？”<br />
<br />
地震中倒塌的校舍多为中小学，以入学年龄论，遇难学生绝大部分出生于1990―2000年之间，这是一个失掉的十年。<br />
<br />
在什邡市洛水镇李冰村，他看到了洛水中学学生的墓地，政府所立的墓碑上，刻有100多位遇难者的名字――这是他所见唯一的一次。<br />
<br />
艾未未有时候会翻一翻那些照片，看看那些砖头下面的孩子们，然后感叹当初的痛苦和激动怎么这么快就被淡漠和遗忘取代。“其实他们就是我们，我们就在下面。这些孩子永远不会去想这个房子的质量有问题，不会去想这里面有没有放进水泥。但我们知道这个事以后，如果不出来说点话，那么你认为我们是什么东西？我们还不得被笑死啊？我想我就是不愿意再被嘲笑罢了。”<br />
<br />
他说自己很少运用语言的策略，但直白如他，却“从没喝过纳税人的茶”，他承认，艺术家身份使得自己不容易被扣上一些帽子――在中国，它们常常被用作打击批评者的武器。“我是做艺术的，我做的所有东西都和艺术有关。艺术是一种有效的处理问题的方式，而在这个原则下，最直接最正面的处理就是最有效的……这次行动中的艺术性在哪里？就是你搭了一个框架，在这个框架里，人的想象力和人的情感，发生了变化。”<br />
<br />
临告别时，艾未未正在摆弄自己的手机，日本《产经新闻》记者打电话过来要采访此事，但电话两次莫名其妙地断掉了。<br />
<br />
南方人物周刊记者杨潇发自北京<br />
<br />
附资料：<br />
<br />
2008年5月24日，国新办举行的汶川地震灾害和抗震救灾情况第14次发布会上，国新办新闻局副局长鲁广锦面对媒体提问死伤学生数据时曾答复说，数字正在统计当中，“有了结果，我们会及时公布的。”<br />
<br />
但随后关于此事，再无官方的正式消息和数据对外通报，外界也未曾听闻有相关责任人依法受到惩处。“最新”的数据，仍停留在2008年5月21日，四川省教育厅厅长涂文涛在教育系统内部会议上的通报：四川省教育系统共死亡6581人，其中学生死亡6376人；1274人失踪，1107人被埋。<br />
<br />
2009年3月8日，地震300天后，在全国两会新闻发布会上，针对记者提问，全国人大代表、四川省常务副省长魏宏表示：“汶川地震最终的死亡人数，我们必须按照国家有关部委对死亡人数特别是失踪人数的有关规定来进行，涉及很复杂的工作和过程。因此在遇难者数字没有最终确定之前，对遇难学生人数也很难给出准确的数字。”<br />
<br />
对于公共建筑的质量问题，魏宏没有提及，只是回答说，地震伤害、地震灾害带来的实际破坏烈度普遍大于当时所有灾区学校设防烈度的1到2度，这是学校等公共建筑大规模倒塌的主要原因。（据财经网）</div>

							
							
							
							
