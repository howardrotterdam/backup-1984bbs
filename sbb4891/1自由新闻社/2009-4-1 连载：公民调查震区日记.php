二、<br />
<br />
我们走访了天河小学分校区，魏老师透露，该校教学楼全倒塌。我们又从杨校长那里确认了该校无遇难学生。然还没从这个值得喜悦的讯息中平静下来，我们又一次被请上了警车。后被带到绵竹富新派出所，强留近七个小时。这一回，我的特殊身份证不灵了，原本属于每个公民的基本的权力只是个屁，在学校所学的法律知识也帮不了我，难道因为今天处理我们这事的还有方镇长吗？方镇长是富新镇的一位抗震英雄。作了询问记录后，李所长在一派胡言中竟还威胁我说，如果我们不在今晚离开德阳，他将联系我们当地的警方来配合将我们遣送回去。看来，法律在他们的面前狗屎都不如。<br />
晚上七点半，我和小黄终于由一名民警从绵竹带到成都火车站，当我们重新踏下自由之地那一刻，我看了看手机的时间：21:19。<br />
广东公民.瑞豪.3月31日。<br />
附，天河小学总部有三名遇难学生，然在前往天河小学总部的路上被警车拦截。<br />
<br />
阳光帅哥瑞豪电话告诉我，他开始感觉到爱上了这个地方。这是多么不容易啊。<br />
<br />
