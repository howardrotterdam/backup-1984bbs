一、 <br />
<br />
3月30日中午，我们到达了绵竹富新初级中学（原富新职业初级中学），登记个人姓名后，我们被允许进入现由活动板新搭建的校园，找到副校长办公室。经介绍，对方是该校的副校长，我们向他表明自己只是个体公民，希望对方可以给我们提供该校已知的两名（据学生调访了解）5.12所遇难学生的信息，被断然拒绝。该副校长对我们说，必须持有上级的批文，才能获得这些名单。我问这是上级对该校的规定，还是该校自己的决定，副校长说，上级没有这样要求，可是这事情属于学校的机密工作，不能提供。不知是什么原因，校长除了对我说非常感谢人民对灾区的关心以外，还在我们面前，歌颂了许多政府和党的功绩。说罢，便起身请我们出去。我见还有其他随后前来此办公室的学生家长，便想从他们那里得到一些具体的信息，前去咨询时，副校长迅速礼貌地将我拉开，但我还是抓紧最后的时机向两位学生家长保证，我们会在校外等他们再详谈，见他们点头，我便随副校长的要求离开校园，走出校门时，当我回头正准备感谢这位校长，抱歉自己刚刚有劳了校长亲自送我们出门，可没想到，这位我本以为还有最后一点风度的绅士，正在费劲地要将本来是敞开的大门关闭，原来他是想亲自过来关门的，我又自作多情了。见他把门关得密不透风，我怀疑他是不是担心我眼神会伤害到了他的学生呢？在校外，我们等了大慨十分钟，一辆警车就停在我们的面前，警察一边登记我们的身份，一边训导我们，我却还向他打听受难学生的情况，聊着聊着，突然见有一名学生家长从学校出来了，我已不顾要配合警察的工作，追到学生家长的面前，正准备向他咨询相关信息，学生家长只说了一句：“没空”，就走了。我愣了一下，无奈回头向警察笑了一笑，然后就问警察，希望他能免费把我们送到另一受灾学校，得逞。不幸是，途中，被另一位高级一点便衣，命令他带我们回到原住的绵竹粮贸招待所，整个下午就我们就被稳定在那里，又是登记，又是查收，来了市政府的团委余姐姐，又来国安局肖警官。使我有错觉自己好像已经成了这个社会的敌人了。所幸，感谢我父亲给我的特殊照顾，感谢我的女友教会我一些简单的法律条规，感谢这些警察还未更进一步的不文明。经过几个小时的对话，我们又可以开始正确地工作了。<br />
