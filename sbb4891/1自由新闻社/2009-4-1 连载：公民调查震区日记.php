<br />
公民调查汶川小组阶段总结<br />
 <br />
我(冬去）以及华耀、劲松、小杨一行四人于3月28日上午，前往汶川县映秀镇开展公民调查实地走访工作。<br />
 <br />
映秀镇地震受灾严重，我们到达以后，对全镇的概况进行实地了解.实体建筑已所剩无几，映秀中学的废墟被作为“地震遗址”而保留了下来，目前可以自由参观。映秀小学由于严重的质量问题已经变成一片废墟，唯一矗立的是学校操场上的一个旗杆。我们走在灾后兴建的“映秀大道”上，一位约40多岁的妇女拉我们住宿，向她打听遇难学生和老师的情况，得知她有一个远房亲戚是映秀中学老师。在距离映秀中学不远的岷江边有一排板房，我们住下。在我们的隔壁，住着一个警察，他对我们警惕性不高，没有构成威胁。<br />
 <br />
午餐在一家灾民开的小店里，联系到了李老师，和李老师聊天，我们表明此行的目地的，希望能提供映秀中学遇难学生的名单，李老师犹豫了，李老师说，只能提供学校遇难学生的总数，52人。他说很忙，有遇难学生家长在等他处理清明的事宜。老师离开后，餐馆老板和我们攀谈，方知李老师是映秀有名的英雄老师，他的儿子在地震中遇难，夫人是医院护士，在地震中参与抢救工作，儿子在废墟中被找到，但是由于他们两夫妻都在工作岗位上忙碌着，没有顾及让自己的儿子先上飞机送往成都，最后由于伤势过重而遇难。李老师不愿再向我们提及学生的事和个人经历有关系。<br />
 <br />
我们当天下午决定把四人分为两组，我（冬去）和华耀一组。以大学生志愿者的身份进行对遇难家长的实地走访。劲松和小杨找渠道拿大名单。下午的工作中，映秀小学的受灾情况很严峻，死亡约300多人，我们得到了校长谭国强的电话。谭校长说他不在映秀在都江堰。我们了解到谭校长身处极大的悲痛之中，不会提供给我们具体名单，谭校长在地震发生48小时后和政府救援进入映秀后意识发生了很大转变，政府已经专门给他上过课。<br />
<br />
一个家长回忆，谭国强校长是个好人，地震发生后三天他都没有离开映秀小学，他的妻子与岳母在地震中都已遇难。地震发生前，学校方面和家长方面都知道映秀小学存在严重的建筑质量问题，乃至后来被鉴定为危房，为此，谭国强校长专门给家长开过会，会的主旨就是关于学校的危房问题，谭校长说他给有关方面写过很多信反映学校危房的问题，但是都得不到回复，后来只有采取无奈之举，限制学生在学校逗留的时间，下午2点上课，中午学生不能在学校逗留，只允许距开课20分钟时才让学生进入学校。说到此，学生家长流下眼泪。<br />
 <br />
对学生家长的走访效率是比较低的，一是由于在安置板房到处摸索遇难学生家长的住处的困难，二是遇难学生家长部分还处在失去子女的悲痛当中，不愿意向人提起这件事，现实的无奈和心底的悲痛对他们形成了严酷的打击,这是我们在走访过程中遇到的阻力。劲松和小杨通过村民，得到了居民点马英的消息，马英是居民点工作人员，负责整个映秀死难学生家长物资发放工作，我们去了她的办公室，马英接待了我们，详谈时得知到大名单的信息很全，是整个映秀遇难学生的全部名单，死难学生姓名、学校、家长、联系方式都记录在案。介于我们的身份，马英说她没有权利向我们提供这份名单，说镇政府办公室的李玉梅那里也有一份名单，把我们推向李玉梅那里，经过僵持，无果。我们只好离开。<br />
<br />
我和华耀去镇政府找李玉梅了解情况，李玉梅否认了她有学校名单的事，说她手上不会给出任何有关遇难学生的信息，除非得到政府的通知，或者领导的许可。下午，又去村委会了解情况，一个年青女职员接待了我们，她的警惕性非常高，得知我们的身份后说，你们做这事的目的不明确，怕引发起群体性事件，解释她是没有这个名单的。<br />
 <br />
3月30日下午，我们到映秀山坡上渔子溪村安置点了解情况，我们得到了一份渔子溪村遇难学生名单，并一一核对遇难学生信息，3月30日晚,回到成都整理资料，把得到的遇难学生名单和照片进行整理发回北京。劲松和小杨在映秀小学废墟拾得的两个学生书包邮寄。<br />
 <br />
成都一晚后，回到了映秀。得知什邡小组处境紧张，带着调查资料离开映秀返回成都。4月4日清明节，映秀公墓（万人坑）举行小规模祭奠5.12遇难者的追悼仪式，我和劲松进行拍摄记录。<br />
<br />
<br />
公民调查 2009年4月6日成都<br />
