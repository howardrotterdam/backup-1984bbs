<br />
北京好： 今早前往蓥华镇，几个孩子给我们提供了这些名单，谢谢xx、xx、xx、xxx。至此，什邡市蓥华镇两所学校：蓥华小学和蓥华初级中学，现据了解小学没有死亡。中学死亡81人，已核实名单65人，包括原有名单4人。4人中补充了年龄和班级。其余的16人正在核查。另外，红白学校12人名单，与已有名单重复，补充了年龄班级等。<br />
