<br />
各省、自治区、直辖市党委教育（高校）工委、教育厅（教委），新疆生产建设兵团教育局，部属各高等学校党委：<br />
<br />
&nbsp; &nbsp; 据有关部门掌握，2005年以来香港“乐施会”中国分部一直与境内部分“维权”组织合作开展“大学生志愿者”培训项目。近期，该机构决定采取“互联网群发”方式，将招聘信息直接发布到境内各高校指导中心，鼓励院校教师推荐人员，再由其筛选出“合适人选”安排到各大城市有合作关系的“维权”机构实习。实习期为2010年3-6月。<br />
