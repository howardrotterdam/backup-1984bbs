<br />
&nbsp; &nbsp; 二、要求高校就业指导中心及各院（系）不得以任何形式为所谓培训项目推荐人员。如发现师生已参与所谓培训项目，要立即采取妥善措施加以劝阻，并做好教育引导工作。<br />
<br />
&nbsp; &nbsp; 三、规范学生社团活动审批程序，坚决防止香港“乐施会”及其合作机构以资助、赞助等形式，在校内宣传造势。加强对毕业生就业实习的组织管理，认真做好就业指导服务，进一步加大对特殊群体毕业生的就业帮扶力度，防止毕业生到所谓“维权”机构“实习”。<br />
