作者 台北特约记者 陈民峰<br />
<br />
发表日期 29/07/2009 更新日期 29/07/2009&nbsp;&nbsp;13:05 TU<br />
<br />
大陆「海协会新闻交流团」访台期间提出两岸新闻媒体互设常驻机构的建议，但双方的看法似乎还有很大差距。台湾陆委会官员透露，28日的座谈会中，双方就这个议题交换意见，没有形成共识。估计大约要到明年第五次江陈会谈，有关两岸新闻、文化、教育等层面的交流事项，才会列入议题。<br />
<br />
海协会新闻交流团目前正在台湾访问，台湾陆委会副主委赵建民透露，28日的座谈会上，台湾提出几项建议，包括大陆能不能「不封锁」台湾的新闻网站？不干扰台湾电台的电波？以及台湾的记者穹霉际会议的时候，时常因为大陆干预遭受到「差别对待」能不能改善？等问题。台湾的这些要求显然将了大陆一军。<br />
<br />
赵建民还说，目前台湾已经开放大陆的旅游、地理等四类杂志，可以用正体字在台湾发行，因此也要求大陆「对等开放」，让台湾同样的四类杂志，可以在大陆用简体字发行。台湾甚至提出让大陆更难以招架的问题，包括两岸的报纸期刊相互在对岸发行，以及两岸的电视节目相互落地营运等。 <br />
<br />
杨毅则表示，希望两岸本著先易后难，循序渐进的方式，「能做的先做起来」，推动两岸媒体互设常驻机构。台湾陆委会则表示，对岸要求的媒体常驻机构，涉及由现行的个人驻点穹茫转变成为机构的设置，还有很多问题有待解决。</div>

							
							
							
							
