2009.12.19<br />
<strong>习近平到访前柬埔寨决定驱逐22名维吾尔人</strong><br />
<br />
 <img src="http://i48.tinypic.com/29x8ccm.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
中国国家副主席习近平<br />
<br />
中国国家副主席习近平将于本周日访问柬埔寨，这是他此次亚洲之行继日本、韩国和缅甸之后的最后一站，届时习近平将与柬埔寨首相洪森、国王西哈莫尼等领导人进行会晤。一直以来柬埔寨和中国都有着十分密切的经济联系，但前不久从中国前去柬埔寨寻求政治庇护的22名维吾尔族人使柬埔寨一度陷入两难境地。本周六，柬埔寨政府宣布将驱逐这一批维吾尔族人。柬埔寨内政部一名发言人表示，这些人将在一周内被驱逐，目的地可能是中国。<br />
 <br />
<br />
根据路透社的报道，中国多年来是柬埔寨的最大外国投资者，投资总额高达43亿美元，项目包括水利、农业、电信和旅游等各个领域。两国去年的贸易额度就达到了7.94亿美元。同时，中国也是柬埔寨的最大援助国，分别在2007年和2008年时向柬埔寨提供了6亿和2.6亿美元的援助，帮助柬埔寨建造公路、水坝等基础设施。另外，中国也向柬埔寨提供军事援助。并且历史上，中国还曾同柬埔寨红色高棉及其领导人波尔布特关系密切。<br />
<br />
此次习近平的到访预计还会带来14个签约项目，关系到基础设施建设，援助以及贷款资金。此前，柬埔寨已从中国得到了10亿多美元的直接投资，还有今年十月刚达成的8.53亿美元用于水利等基础建设的贷款项目。<br />
<br />
面对中国的大力投资和援助，同时也是习近平即将到访之际，柬埔寨政府却一度面临着外交挑战。原因就是此前到柬埔寨寻求政治避难的22名维吾尔族人。<br />
<br />
这些维吾尔族人在几周前从中国来到柬埔寨，向驻金边的联合国难民署寻求庇护。柬埔寨是签署1951年难民公约的两个东南亚国家之一，并承诺不会遣返回到本国后将遭到起诉的庇护寻求者。虽然中国政府并没有公开表示要求将这些人引渡，但是中外交部发言人姜瑜在本周的例行记者会上曾表示，这些维吾尔族人涉嫌犯罪，有关部门正在调查之中，并表示国际难民保护体系不应该成为犯罪分子逃避法律制裁的庇护所。<br />
<br />
但是，习近平到访前夕，事件突然出现变化。柬埔寨外交部一名发言人周六表示，柬埔寨政府决定驱逐一批寻求政治庇护的中国维吾尔族人，原因是，他们是非法入境。据悉，其中的两人已不知去向。这名发言人没有说明，这批维族人将于何时被送往何处。柬埔寨内政部一名发言人表示，这些人将在一周内被驱逐，目的地可能是中国。<br />
<br />
总部位于海牙的非联合国会员国家及民族组织的塞斯・霍克曼此前向路透社表示，柬埔寨和中国之间的经济联系不应该影响到难民寻求庇护的过程。美国维吾尔协会的研究员艾米・瑞格也曾表示，相信柬埔寨会遵守所做出的国际人权承诺。而大赦国际则发表了一封公开信，希望柬埔寨政府拒绝任何引渡的要求，并举例称此前一些已获得难民身份的维吾尔族人回国后仍然遭到起诉及审判的案例。<br />
<br />
联合国难民高级专员办公室亚洲发言人麦肯锡日前表示，如果这些维吾尔人被确定为难民，那柬埔寨政府就应该遵守1951年难民公约的内容不能将他们驱逐。<br />
<br />
在得知柬埔寨政府做出驱逐决定后，联合国难民署办事处一名发言人称，周五晚间，该机构尚请求柬方不要将这些人驱逐出境。此前，该机构一直在帮助柬埔寨有关当局确认这些人的身份。<br />
