<br />
<br />
　　对比该快餐店几个月来的传单可发现：每个月都有不同的菜式涨价，有些便宜的菜式甚至直接“消失”，如2月份的菜单中还能找出3个10元以下的菜式，到了5月份就只剩下一个番茄炒蛋低于10元了。老广最喜欢喝的老火靓汤，从4月份开始集体涨价0.5元。而这种情况不止出现在一家快餐店，记者调查发现，烧鹅仔、华辉拉肠、三禾百味等多家茶餐厅也有不同程度的涨价。华辉拉肠的招牌粥，4月份开始由3.5元/碗集体涨到4元/碗。都城快餐的玉米汁从最初的4元/杯也涨到了现在的5元/杯。<br />
<br />
<br />
<br />
　　“别看是一两块地涨，一个月下来单单快餐就要多花三四十块钱。”杨小姐叹了口气。<br />
<br />
<br />
<br />
　　白领王小姐准备摆酒，结果发现在大型酒楼里已很难找到2000元一桌的宴席了，只有越秀区某酒店一款简单的宴席是1980元/桌，其同事去年年底结婚时，这款宴席仅是1780元，并且包含了礼堂装饰等诸多优惠，“没想到早结婚也有‘着数’啊！”王小姐最后只好忍痛订了一款2000多元/桌的宴席。<br />
<br />
<br />
<br />
　　酒楼<br />
<br />
<br />
<br />
　　原材料多数在涨价<br />
<br />
<br />
<br />
　　对快餐持续涨价感到无奈的不仅仅是消费者，就连餐厅负责人也表示无法承受。都城快餐陈经理指出，原材料涨价是快餐涨价的源头，很多成本经营者都想办法消化了，实际上原材料涨价的幅度远高于快餐的涨价速度。<br />
<br />
<br />
<br />
　　陈经理介绍，今年以来，油、米、菜都在涨价，供货商一个月换一次价格，比如大米，去年年底是98元/袋（50公斤装），元旦一过就涨到100元/袋了，现在每袋已涨到103元了，青菜更加不用说，一天一个价，尤其是4月份每天都阴雨天气，菜价奇高。“原材料统统涨价，每个月多出几万元成本，做快餐也基本没什么利润了。”陈经理说。<br />
<br />
<br />
<br />
　　烧鹅仔快餐店店主黎先生也告诉记者，由于原材料涨价，不得不以提高价格的方式来维持经营，从3月份开始就取消了“免费加饭”的优惠，“我们也不希望涨价，但如果接下来原材料继续上涨的话，还是被迫得跟着涨，希望国家可以出台政策来抑制粮油涨价现象。”<br />
<br />
<br />
<br />
　　柴米油盐“涨”声一片<br />
<br />
<br />
<br />
　　部分品种的青菜，价格比去年同期上涨超过五成<br />
<br />
<br />
<br />
　　记者了解到，伴随着蔬菜、水果、杂粮等日常生活用品的普遍“高烧”，通胀再度成为笼罩在老百姓头顶上的阴霾。<br />
<br />
<br />
<br />
　　蔬菜价格总体上升<br />
<br />
<br />
<br />
　　广东省物价部门前日公布，受去年冬季气温偏低、今年开春后“倒春寒”天气持续以及生产流通成本增加等因素的影响，今年以来省内蔬菜市场价格总体上升。<br />
<br />
<br />
<br />
　　昨日记者走访了市区一些肉菜市场发现，虽然前段时间部分蔬菜价格有所回落，但与去年同期相比不少品种的价格仍是上涨。在冼村肉菜市场，2元（单位为500克，下同）以下的蔬菜少之又少，只有油麦菜和生菜是1.8元，这两种最普通的青菜在2007年曾低至0.3元；去年同期只卖1.8元的通心菜目前的价格是3元，西兰花去年同期的零售价仅是3元左右，而近一个月来都保持在4.5元的高位。<br />
<br />
<br />
<br />
　　粮油价格不断上涨<br />
<br />
<br />
<br />
　　在许多东西涨价的压力下，作为生活必需品的大米和食用油也在不知不觉中微涨。记者昨日从广州多家超市了解到，去年至今大米价格至少上涨了0.5元，食用油价格也有所上涨。<br />
<br />
<br />
<br />
　　在宏城超市，一袋20斤装的马坝油粘米特价还要64.8元，这款大米去年原价只要58.8元，而且经常以49.9元促销。<br />
<br />
<br />
<br />
　　食用油方面，橄榄油等高端食用油的强势进入市场不但没有引起其他品种食用油的降价，反而带动了食用油的涨价。在好又多超市，去年只卖45元/桶（5升装）的某品牌食用调和油，现在正以49.8元的特价促销，原价则需要54.5元，涨幅将近10元。<br />
<br />
<br />
<br />
　　煤气暂无涨价压力<br />
<br />
<br />
<br />
　　价格长期保持稳定的天然气也盛传将涨价。<br />
<br />
<br />
<br />
　　近段时间来，国内不少城市纷纷上调了管道天然气的价格。幸好，广州的天然气目前还没有涨价的压力。<br />
<br />
<br />
<br />
　　预测<br />
<br />
<br />
<br />
　　物价走势 短期不容乐观<br />
<br />
<br />
<br />
　　下周二（5月11日），国家统计局就将公布4月份全国居民消费价格指数（CPI），其在3月份短暂回落后重拾升势已是各方普遍认可的观点。<br />
<br />
<br />
<br />
　　国家统计局在全国50个城市进行的调查结果显示，4月11-21日，大白菜价格比3月份月同期上涨约40%，西红柿、土豆分别比3月份同期上升10%-13%。<br />
<br />
<br />
<br />
　　专家分析，蔬果价格上涨只是表象，深层次原因的是上游生产成本攀升。这些成本包括农药化肥、运输、储藏成本，还有劳动力成本。专家认为，国际大宗商品价格振荡走高加大了输入型通胀的压力。4月最后一天，国际原油价格突破86美元/桶，市场估计，今年的目标价很可能在100美元/桶上下。另外，劳动力成本由于今年初的“用工荒”也明显上涨。5月1日起，广东开始执行新的最低工资标准，平均增幅达21.1%，广州增幅达19.8%。这也将令生产成本有所增加。<br />
<br />
<br />
<br />
　　目前不少专家表示，受大宗商品价格回升、资源品涨价等因素影响，今年第二、第三季度物价形势都不乐观。<br />
<br />
<br />
<br />
　　著名经济学家谢国忠预言，尽管在官方统计数字里前3个月CPI只有2.2%，“但通胀可能很快迈上两位数。”</div>

							
							
							
							
