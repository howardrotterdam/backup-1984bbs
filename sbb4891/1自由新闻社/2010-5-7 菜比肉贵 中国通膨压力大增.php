受到天灾与人为炒作的双重影响，近期中国各地蔬菜价格急剧上扬，部分地区的菜价甚至超过猪肉价格，势必加重中国官方今年控制通货膨胀的压力。<br />
<br />
身为中南海重要智囊的中国社科院警告，从当前状况来看，今年中国粮食生产价格的涨幅可能达10%.<br />
<br />
香港经济日报与大陆每日经济新闻报导，年初以来，东北地区出现大雪，华北遭遇“倒春寒”（晚春气温低于正常水准）天气，西南、山东、河北、辽宁和安徽等重要蔬菜产地相继受到大旱、狂风和暴雪等天然灾害侵袭，造成蔬菜产量大幅下降，供应开始短缺，导致中国各地蔬菜价格暴涨。<br />
<br />
以安徽为例，由于大雪天气冻伤当地部分蔬菜，导致菜价高涨。在省会合肥，每斤青椒、土豆价格各为人民币4.5元和6元，较去年同期上涨一倍，并超过当地猪肉每斤4.1元的价格。当地大白菜为每斤3元，直逼猪肉价格。<br />
<br />
西部大城重庆的大蒜价格，从去年4月的每斤人民币0.2元，飙升至每斤6.5元，等于一年内上涨逾30倍，创下当地大蒜价格历史纪录。<br />
<br />
大蒜涨价原因一方面是当地大蒜存量少，加上因“倒春寒”导致山东、河南、河北等大蒜主要产区减产，使得货源缩减或上市。北京、天津等20多个省市的大蒜价格，4月也上涨明显，涨幅在20%至40%之间。中国各地菜价飙涨的成因，在天灾之外还有“人祸”。<br />
<br />
近几个月中国官方接连推出房市调控政策，加以中国股市持续低迷，使得温州炒房团和山西煤老板们等中国社会游资，从资本市场转入菜市。这些资金主动用大量资本，从原产地收购大蒜等蔬菜，然后统一抬高价格做集中销售，导致大蒜等蔬菜价格大幅上扬。<br />
<br />
在这波涨价潮中，本应受惠的中国农民由于驾驭市场能力很弱，无法及时反应和预判，最后受益反而是市场炒家。<br />
<br />
中国国家统计局数据显示，今年菜价较去年同期涨逾10%.中国社科院最近发布的《农村经济绿皮书》预测，中国今年粮食生产价格可能涨5%，进而带动菜价总体上涨4%.<br />
<br />
担任《农村经济绿皮书》主笔之一的中国社科院农村发展研究所副所长李国祥更说：“从目前的情况看，全年粮食生产价格涨幅很可能超过10%.”<br />
<br />
由于菜价上涨直接带动食品价格上扬，后者又是消费者物价指数(CPI)主要刺激因素，菜价上涨将推升CPI指数，造成中国通膨更为严重。<br />
<br />
