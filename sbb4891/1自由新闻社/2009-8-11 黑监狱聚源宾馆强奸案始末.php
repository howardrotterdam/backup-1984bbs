													<h2>上访女孩李蕊蕊被保安强奸</h2>
						
						
																											<div id="postmessage_400739" class="t_msgfont"><a href="http://view.news.qq.com/a/20091225/000033.htm" target="_blank">http://view.news.qq.com/a/20091225/000033.htm</a><br />
（原标题：上访者李蕊蕊的黑色炼狱）<br />
李蕊蕊已经睡了三天的马路。一到北京，她所带的微薄旅费就给弄丢了。<br />
毗邻北京火车南站，设施简陋，一晚130元，聚源宾馆对于李蕊蕊来说，不啻为一家住不起的“豪华”宾馆。<br />
这个价格似乎没有招来多少贵客。在这家灰色院落的简易宾馆里，已经住着70多名特殊“住客”。他们跟李蕊蕊有着类似的目的和旅程：上访，流浪街头，遭警察审讯，最后被强送到这家专门分流遣返上访者的宾馆。<br />
8月3日，李蕊蕊也被“送”进了这里。这家不需要付房费的宾馆，却成了她的黑色炼狱。在入住5小时后，21岁的李蕊蕊，遭到一名叫“小强”的看守的强暴。隐忍的愤怒被引燃了，这群特殊“住客”冲破了防线，纷纷逃离。<br />
“那里让我害怕。”8月8月，李蕊蕊回到了安徽界首市大黄镇的家中。那里隶属阜阳，距离北京855公里，一个节奏缓慢的小镇。家让她感到安全，但是受了刺激的她，与之前相比，更为迟钝和不安。<br />
面对南都周刊记者的采访，口齿有点不清的她，缓慢地说：“我不会再去北京了。”这句话，她反复说了两遍。<br />
孩子气的上访冲动<br />
7月30日，李蕊蕊的父母发现，女儿失踪了。母亲王相莲很担心，女儿出门连手机都没带，况且“女儿智力发育有点慢，口齿也不清楚”。<br />
此时，李蕊蕊正在火车上。经过10个小时的火车旅程，7月31日上午，她到了北京。<br />
“我去上访。”她这样定义自己的北京之行。上访的理由，在记者看来，多少显得很孩子气。<br />
1988年出生的李蕊蕊，8岁上小学，中间留过级，一直到2006年，还在镇上一所中学读初二，和弟弟一个班。那年她已经18岁，周围都是比她小半个头的同学。无法忍受同学嘲弄的她，决定辍学回家。<br />
这成了她的心病，生活总是不甚满意。辍学后的李蕊蕊，能做的就是在集市上卖点水果。这期间，她订了一门亲事，男方在上海打工。但她对这门撮合的婚事并不热情。<br />
她把这一切归咎于自己在上学时，遭到了同学的欺侮还有老师的怠慢。她决定讨个公道。<br />
怎么想到去北京讨说法，这是连李蕊蕊都无法解释的事。她对北京的了解，仅限于电视里那些不完整的片段，但她有了冲动。<br />
她缺乏出门的经验，最远只到过离家40公里的阜阳市，意味着这是一趟风险未知的旅程。作为世界上最大的城市之一，北京有让人头晕目眩的立交桥，也有迷宫般的庞大街区。<br />
一到北京，李蕊蕊就发现一个不妙的事情：钱丢了。到北京的最初3天，她只好睡在马路边。虽然“3天里只吃了一顿饭”，但是她说，自己很兴奋。<br />
如果说她是一名上访者，不如说是游客来得合适。8月3日，李蕊蕊逛到了天安门广场。这是中国的中心，聚集了各地最虔诚的游客、背包客，还有上访者。他们兴奋而来，最终留在记忆中的，只是一张以悬挂毛主席像的城楼为背景的快冲照片。<br />
而李蕊蕊收获的却是一次警察的审问。这天下午，李蕊蕊做出了一个自己也无法解释的举动：翻过栅栏，进入了天安门广场东侧的最高权力机关人民大会堂院里。遍布角落的监控探头，在最短的时间发现了这个瘦小的身影。警车把她带走了。<br />
李蕊蕊的解释至今听起来也有点荒诞。她称，自己在问路时遭到了大会堂门前一名武警的欺骗，于是准备找对方理论。<br />
富有经验的天安门广场警察，很快判断出了李蕊蕊的身份：上访者。傍晚时分，三个陌生男人出现在派出所。李蕊蕊发现，这些男人操着和自己相近的口音。<br />
“他们说是安徽阜阳驻京办的。”李蕊蕊回忆称，这三个男人从派出所接走了自己，一路上没再搭理她。<br />
晚上10点多，她被带到了一个叫“聚源宾馆”的地方。<br />
聚源宾馆噩梦<br />
聚源宾馆位于北京丰台区马家堡23号院，不远处就是北京火车南站。灰色院落，从外表看没什么特殊之处，周边这种不起眼的小旅馆，还有很多。<br />
带她来的三个男人，向宾馆女老板交代几句后就走了。女老板把李蕊蕊带到客房走廊尽头，右转进入一个木门，推开木门，里面有一层铁门。敲门，一个年轻人打开门，里面是一间房子，摆了四张床，再往里又有三个套间，全是上下铺。<br />
这里和外面客房是截然不同的区域。李蕊蕊后来得知，当天，陆续有河南和安徽的上访者被送到了这里。<br />
比李蕊蕊早到的安徽籍上访者彭光发说，聚源宾馆是一刘姓河南人开办的，把里面的房间出租给了河南桐柏县驻京联络办。“这是驻京办的一个职责，就是拦截上访者，然后遣送回原籍。”<br />
这是门不错的“生意”：地方政府不希望过多的本地人去北京上访，这影响政绩和脸面。<br />
女老板把李蕊蕊交给了一高个男青年后，就关上了那道铁门。这个有1.8米高的青年，被聚源宾馆的上访者叫做“小强”。小强把李蕊蕊安排在靠近大门的上铺，他自己睡在下铺。<br />
彭光发说，小强是雇来专门看守上访者的。“他们总共7个人，都是河南南阳的，对我们可凶了，经常动手打。”<br />
小强看李蕊蕊还没有吃饭，提出请她出去吃宵夜。“被我拒绝了。我又不认识他。”李蕊蕊说。<br />
李蕊蕊很快入睡。这个敞开式的大房间，没什么隐私可言。正因此，睡在李蕊蕊床铺对面的彭光发，目击了后来发生的一幕：“将近深夜2点，小强站在铺前抚摸那个姑娘，然后爬上上铺，用一张被单盖上了两人。”彭光发告诉记者。<br />
李蕊蕊说，自己在睡梦中醒来，发现下铺那个高个子青年躺在自己身边，“他先是亲我的脸，又把手插进我的裤子，然后脱下了他的裤子，趴在我身上。”<br />
李蕊蕊的反抗引起了屋内其他人的注意，但没有人前来过问。直到小强下去的时候，李蕊蕊喊道：“你别走，你强奸我。”人们发现李蕊蕊的床单上有一些血迹。彭光发说：“当时她的裤腿就褪在脚踝处，下身光着。”<br />
“谁强奸你？都睡觉去。”小强呵斥道。<br />
现场目击者、河南南阳上访者王叙良说：“当时这个女孩光说下面疼，下身不断往外出血。强奸她的这个人是看管我们上访的，他拿着钥匙，这是他的职业，就是一个打手。”<br />
随后，李蕊蕊被宾馆的一个女工作人员，接到了别的房间睡觉。“她说会保护我。”李蕊蕊说。<br />
8月4日清晨6点多，陆续醒来的其他访民，听说了此事。愤怒的情绪在传播和议论声中被引燃爆发，访民们从指责小强开始。“他低着头，不再吭声。”彭光发说。<br />
随后，一个来自河南的女访民开始砸门，小强等7名看守只是象征性地拦了拦。大家冲了出去，一哄而散。“女访民遭截访宾馆打手强奸”的消息，通过手机短信和网络很快传播。上午10点多，不少网友从各个地方往聚源宾馆方向赶来。<br />
李蕊蕊先是被簇拥着去了附近的洋桥派出所报案。据说小强已跑掉了，警察把同为看守的小强弟弟和另外两名看守带回问话。李蕊蕊抱着沾有血迹的被子，又跟着众人一起去北京市公安局报案。网友赵连海开车把李蕊蕊送到了北京市公安局。很快，国内外媒体记者也介入进来。<br />
到了这天中午，警车出动了，把前来报案的人都带到了东交民巷派出所。赵连海说：“本来是协助来报案的，没想到反而因为‘扰乱公共秩序’被关了一晚上。”赵连海从派出所出来的时候，已经是8月5日的下午1点钟了。<br />
遣送回家<br />
8月5日，界首大黄镇的李自付接到了镇派出所所长打来的电话，让他到北京接人。李自付是李蕊蕊的大伯。李自付和李蕊蕊的舅舅王忠诚一起，连夜往北京赶。<br />
李蕊蕊此时已从东交民巷派出所，转到洋桥派出所。据她说，警察让她指认犯罪嫌疑人后，8月4日晚，“就带我到北京第三人民医院妇产科，给我做了‘洗精’。”<br />
次日，警察又带了李蕊蕊去做法医鉴定。李蕊蕊想不明白，“‘洗精’之后再去做法医鉴定，有何意义？”<br />
李蕊蕊称，因为下体一直在出血，8月5日晚，自己又被带到了北京大学第三医院妇产急救科，一个女警察为李蕊蕊带来了新内裤。<br />
李蕊蕊说伤口是被强暴时留下的。她称虽然自己有男朋友，但是之前并没有过性生活。<br />
8月6日晚，警察又带李蕊蕊到精神病院做精神检查。“他们说我是精神病。” 李蕊蕊说。<br />
其他的事，李蕊蕊已经记不清了。因为对北京不熟，所以上述医院究竟在哪里？检查和鉴定结果是什么？她都不知道，“他们不告诉我结果，也没有给我任何东西。”<br />
在懵懵懂懂中，李蕊蕊只记得，自己是在派出所一间小屋度过了3天。<br />
此时，李自付和王忠诚已经来到北京，被界首信访办安排住到了盐湖宾馆。同行的还有大黄镇的镇党委书记。之前，大黄派出所的所长也到了北京。<br />
“政府向我保证，一定要严惩凶手。”王忠诚说。8月7日上午，两人在洋桥派出所见到了李蕊蕊，一点没有耽搁，搭乘一辆镇上来的轿车连忙赶回安徽。<br />
李蕊蕊的北京之行就这么快结束了。在北京的一周，她分别住在街头、聚源宾馆、派出所。那些和她萍水相逢的警察、访民、网友，她一个也不认得，也渐渐模糊了。<br />
似乎什么都没有发生过<br />
如果不去追究，这里好像什么都没有发生过。<br />
聚源宾馆是一栋4层的陈旧小楼，楼道里堆放着换洗的床单，房间里充斥着异味，墙上贴着按摩的价格表。<br />
8月9日，记者探访的时候，那间李蕊蕊呆过的铁门屋子已经人去屋空。“现在没有关上访的了。”前台的服务员一脸木然地说。<br />
这里永远找不到负责人。河南籍老板据说回家了。前台服务员说，是河南桐柏驻京办的在这里长期租了房子，专门拦截安置那些上访者。<br />
一位不愿具名的河南上访者说，关他的那间屋里，住着20多人，空气不流通，喘不过气来，头痛。有好多人得了流感，有些是老弱伤残，吃得也很差。<br />
她称，截访人员害怕他们的违法行为被曝光，安排打手看管，并断绝了访民与外界联系。“我们的手机被抢走了，跟外界联系不上。”这位女访民说。<br />
有说法是，聚源宾馆是一个姓刘的原信访官员开的，这里经常关着几十个失去行动自由的上访者。这是个不错的“生意”。访民称：“宾馆从地方政府手中接待每一个访民收取100元，一年收的钱可能上百万。”<br />
如果考察一下地形，就不难理解聚源宾馆为什么生意兴隆了。从宾馆出来往北不远，陶然桥东北角，这里是中共中央办公厅、国务院办公厅人民来访接待室，同时也是国家信访局来访接待司。方圆几公里每天都聚集了来自全国各地的上访者。<br />
周日傍晚，在陶然桥立交桥下，许多上访者准备在地下通道过夜。他们在等待周一人民来访接待室上班，然后递上各种复杂的上访材料。李蕊蕊也曾经像他们一样露宿街头。<br />
这里甚至衍生了与上访有关的副产品。一个东北女人在街头兜售印刷简陋的“首都上访机构名录电话”。几个上岁数的女人，在游说疲惫的上访者到宾馆住宿。<br />
在聚源宾馆周边，还有大量这样的宾馆存在。在太平街的温馨青年宾馆，服务员告诉记者，“客房已经被河南截访人员的预订走了，都是用来让上访者住宿的。”<br />
上访者的命运是，被截访强送到宾馆，然后由各自属地的信访人员接回家乡解决问题。<br />
长期关注上访者的中国政法大学老师腾彪向记者介绍，他们去年曾经组织许多北京的热心网友，一起探访过包括聚源宾馆、温馨青年宾馆在内的这些收押上访者的宾馆，“情况非常严重”，在举报和解救被关的上访者过程中，志愿者还被野蛮的看守打过。<br />
腾彪说，这种专事截访的宾馆在周边甚至形成了产业。“被关在里面的上访者完全是无辜的，他们在国家信访局或者最高法院等信访部门正常上访时被带到这里，没有经过任何法律手续。”<br />
由于缺乏保护，发生在截访宾馆的伤害事件也时有发生。今年4月，来自山东的访民姚晶被临沂驻京办和青年凤凰宾馆的几个男人打伤，脾内部挫裂，如果不及时治疗可能有生命危险，4月30日到5月2日，临沂官员在压力下一共付了两次医疗费。<br />
在网上，更有许多愤怒的网友直接把这些宾馆称为“黑监狱”。<br />
8月7日，记者跟洋桥派出所联系。一名女警官透露，李蕊蕊案已由刑警办案，记者采访要经过丰台区公安分局批准。丰台区公安分局称，记者采访需要得到北京市公安局新闻办批准。而北京市新闻办则要求记者提供采访提纲。<br />
8月7日，记者在发给北京市公安局新闻办的采访提纲上，罗列了3个问题：上访者李蕊蕊在聚源宾馆被河南看守强奸一事是否属实？案件侦破进展如何？各地驻京办和信访办截访上访者的做法是否涉嫌非法拘禁？<br />
截至记者发稿时，北京市公安局尚未回复。李蕊蕊的舅舅王忠诚说，北京警方已经去河南抓捕嫌疑人小强。他们正准备聘请律师，“起诉李蕊蕊被非法拘禁所造成的伤害问题”。<br />
安徽阜阳驻京办工作人员，接受记者采访时，承认发生过李蕊蕊事件，但是，他对记者说：“这事你没必要了解清楚。”<br />
河南南阳驻京办说，桐柏隶属南阳，但是桐柏县是否有驻京办，这事不归他们管。<br />
李蕊蕊所在的聚源宾馆，据传和河南桐柏驻京办有关。但是打通宾馆提供给记者据说是桐柏驻京办负责人的电话，对方予以否认：“我们是做生意的。8月4日那天宾馆很正常。”<br />
至今还没人出来为这一事件做一个完整和合理的说明。李蕊蕊有点担心，这场噩梦很快会悄无声息地在生活中迅速被抹去。</div>

							
							
							
							
