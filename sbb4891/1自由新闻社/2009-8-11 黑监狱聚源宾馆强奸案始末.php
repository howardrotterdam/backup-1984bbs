　　检方的抗诉理由之一是认为一审判决适用法律错误。检方在抗诉书中称，被告人徐建系在公共场所当众强奸妇女，检察院以其触犯《刑法》第236条第 3款第3项之规定提起公诉，认定徐建属于强奸罪的情节加重，应当在10年以上有期徒刑进行判罚。而一审判决对于徐建在公共场所当众强奸妇女的事实予以了确认，但引用了《刑法》236条第1款进行判罚，属于适用法律错误。<br />
<br />
　　记者查阅我国《刑法》了解到，第236条第3款第3项规定，在公共场所当众强奸妇女的，处10年以上有期徒刑、无期徒刑或者死刑；而根据236 条第1款，以暴力、胁迫或者其他手段强奸妇女的，处3年以上10年以下有期徒刑。检方在抗诉书中称，徐建在公共场所当众强奸妇女，属于强奸罪的情节加重，应按《刑法》第236条第3款第3项之规定予以处罚。<br />
<br />
　　理由二<br />
<br />
　　自首条款适用不当<br />
<br />
　　检方的另一抗诉理由是徐建的自首情节并不能使其获得减轻处罚。检方在抗诉书中称，徐建利用其看管上访人员的身份，在多人居住的房间内强奸李蕊蕊，而李蕊蕊系边缘智力和精神分裂症患者，发案时性防卫能力减低；并且这一强奸行为被当时在屋内居住的多人所感知。后徐建锁门离去，屋内多人破门而出，与李蕊蕊共同报案后又接受了媒体采访。此案一度引起过社会关注，造成恶劣的社会影响。这一过程，足以反映出徐建犯罪行为的社会危害性和对被害人的伤害程度。<br />
<br />
　　检方称，公诉机关虽然对于徐建的自首情节予以认定，但不建议法庭对其减轻或者从轻处罚。一审判决对徐建予以了减轻处罚，因而在10年有期徒刑以下进行量刑。检方认为，一审判决对于自首条款的运用脱离了全案的基本事实和情节，适用不当，因而对于徐建的判罚属于量刑畸轻。综合全案的过程及社会危害性，徐建的自首情节不能予以减轻处罚。<br />
<br />
　　&gt;&gt;马上就访<br />
<br />
　　李蕊蕊就民事部分上诉<br />
<br />
 昨天，记者联系到李蕊蕊的代理律师张荆。她告诉记者，因认为一审判决确认的赔偿数额有误，李蕊蕊已委托她就民事部分提起上诉，请求市二中院撤销一审判决，依法改判或发回重审。她认为，一审法院判决只部分支持了李蕊蕊的直接物质损失2003.9元，而对其心理治疗费、精神损害赔偿各5万元的请求分文未支持，应属认定事实不清，判决有误。<br />
<br />
 张荆说，李蕊蕊遭强奸后，心力交瘁，晚上经常失眠，白天又时常出现幻觉，不敢与人交流，出现严重交际障碍。经诊断，她需要接受长期的心理疏导治疗，而生于农村家庭的李蕊蕊，其家庭微薄的收入显然难以承担这笔必然发生的费用。另外，李蕊蕊生活在思想观念保守落后的农村，其遭受强奸一事使她今后的婚姻家庭生活将面临巨大困难，能够预见到此事对她未来整个人生都将产生恶劣影响。而且，性侵害案件给被害人造成的精神伤害是无形的、终身的、巨大的，这种痛苦是本案有限的医疗费所不能弥补的。<br />
<br />
 “上访的本来目的是要倾诉冤屈，维护权益，却遭到上访管理人员的再次侵权。”张荆在上诉状中称，她希望二审法院判决支持赔偿李蕊蕊心理治疗费用及精神损害抚慰金共10万元。<br />
<br />
　　&gt;&gt;对话・李蕊蕊<br />
<br />
　　“我还在阴影里徘徊”<br />
<br />
　　昨天下午5点，正在家里洗衣服的李蕊蕊与记者在电话中进行了简短交谈，这个21岁的女孩所使用的手机彩铃是《不要再来伤害我》。<br />
<br />
　　记者：检察院就一审判决抗诉了，你知道了吗？<br />
<br />
　　李蕊蕊：知道了，律师告诉我了。抗诉的意思就是说检察院也觉得他判轻了，对么？如果是，那我和检察院的意见是一致的，他的性质太恶劣了。<br />
<br />
　　记者：你现在的心情怎么样？<br />
<br />
　　李蕊蕊：时好时坏的，我还在阴影里徘徊。主要是这里的人都知道了。平时我基本上就呆家里做饭、洗衣服，实在呆不住了才出去玩一会儿。<br />
<br />
　　记者：以后怎么打算呢？<br />
<br />
　　李蕊蕊：没想好啊。想上学，可是我妈说我过年龄了，家里也没钱。我们家靠种地挣钱挣的太少，种些小麦、豆子、玉米、芝麻。<br />
<br />
　　记者：这个案子二审开庭你会来北京吗？<br />
<br />
　　李蕊蕊：想去。上次宣判我就想去，可是没坐上车。不过，去一趟消费太高了，我们家没有钱。这案子春节前能判么，我希望早点判。(记者 裴晓兰)<br />
<br />
