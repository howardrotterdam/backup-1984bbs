													<h2>公开信全文</h2>
						
						
																											<div id="postmessage_417835" class="t_msgfont"><a href="http://crd-net.org/Article/lingbaxianzhang/201001/20100106215733_19315.html" target="_blank">http://crd-net.org/Article/lingb ... 06215733_19315.html</a><br />
<br />
（维权网义工 何贝翻译）今天，是77宪章联署人向当年的捷克政府递交“77宪章”文本33周年纪念日。今天上午11时，77宪章发起人之一、剧作家、捷克民主政府前总统哈维尔先生与其他联署人一起前往中国政府驻捷克领馆，递交一封抗议监禁刘晓波十一年的公开信。中国领馆没人开门出来接受。下面是公开信全文：<br />
<br />
<strong>公开信</strong><br />
<br />
中国国务院<br />
<br />
北京 100032<br />
<br />
胡锦涛阁下：<br />
<br />
12月23日，北京市第一中级人民法院，在不经任何审判而关押一年多以后，以“煽动颠覆国家政权“罪判处了受人尊敬的作家以及人权活动家刘晓波11年徒刑。<br />
<br />
主席先生，我们希望你知道，我们不认为这次审判是独立的司法程序，无论是你还是你的政府都不能干预的司法程序。事实正好相反。刘先生的审判是一个你最终担负政治责任的政治命令的结果。我们相信，这次仅仅由于批评性地思考与谈论各种政治和社会问题，而对一个受人尊敬的、著名的、杰出的公民的审判，主要是为了严正警告其他人不要追随他的道路。<br />
<br />
33年前的1977年1月6日，我们，剧作家瓦茨拉夫・哈维尔，演员帕维・兰多夫斯基，作家卢德・维克理克，也被我们自己国家――一个一党执政的社会主义国家的警察逮捕，然后被以同样的“罪行”被审判：因为起草了《七七宪章》并收集了签名，目的是要求我们自己的政府尊重我国的宪法、国际义务以及基本的公民权利和人权。后来，我们当中一些人也被来自于政治命令的司法程序判处了长期徒刑，就像北京法院无耻地在2009年12月判处刘晓波先生一样。<br />
<br />
我们坚信并敢于要求你和你的政府，当知识分子、艺术家、作家和学者行使自己的核心使命：即思考、重新思考、提出问题、批评、创造性地采取行动并尝试开展公开对话时，根本就不存在颠覆国家安全。相反，当政府压制知识分子的表达时，现在和未来的社会福祉就会被政府破坏。<br />
<br />
当公民按照自己的意志、通过自己的知识和良知采取行动时，当公民和平地相互结社、讨论和表达他们对社会将来发展的关心与观点时，根本就不存在颠覆国家安全。<br />
<br />
相反，当一个国家的公民不允许自由地采取行动、结社、思考与表达时，这个国家未来的财富和精神就会被破坏。<br />
<br />
这就是为什么我们呼吁你和你的政府，当法庭审理刘晓波的上诉时，保证给予刘晓波一个公平而真正公开的审判。<br />
<br />
我们也要求你和你的政府，结束对其他《零八宪章》签署者的软禁和警察监视。我们呼吁你和你的政府，结束因言获罪，并释放所有政治犯。<br />
<br />
主席先生，我们希望你知道，我们会继续密切关注刘晓波先生和其他《零八宪章》签署者。我们将与很多来自捷克共和国和斯洛伐克的《七七宪章》签署者，采取继续和持续的努力，提请国际社会注意他们的处境。<br />
<br />
问候，<br />
<br />
瓦茨拉夫・哈维尔，剧作家<br />
<br />
帕维・兰多夫斯基，作家<br />
<br />
瓦茨拉夫・马里，布拉格主教<br />
<br />
布拉格，2010年1月6日<br />
<br />
His Excellency Hu Jintao<br />
President of the People’s Republic of China<br />
State Council<br />
Beijing 100032<br />
P.R. China<br />
<br />
<strong>Open Letter</strong><br />
<br />
Prague, January 6, 2010<br />
<br />
Your Excellency, <br />
<br />
On December 23, the Beijing Municipal No. 1 Intermediate People's Court - after holding him for over a year without trial - sentenced respected intellectual and human rights activist Liu Xiaobo to 11 years in prison for &quot;inciting subversion of state power.&quot; <br />
<br />
Mr. President, we would like you to know that we do not consider this trial an independent judicial process in which neither you nor your government can interfere. In fact, it is just the opposite. Mr. Liu's trial was the result of a political order for which you carry ultimate political responsibility. We are convinced that this trial and harsh sentence meted out to a respected, well-known, and prominent citizen of your country merely for thinking and speaking critically about various political and social issues was chiefly meant as a stern warning to others not to follow his path. <br />
<br />
Thirty-three years ago, on January 6th, 1977, we, playwright Vaclav Havel, actor Pavel Landovsky, and writer Ludvik Vaculik, were arrested by the police in our own country, then a one-party Communist state, for &quot;committing&quot; exactly the same &quot;crime&quot;: the drafting of Charter 77 and collection of signatures with the intent to call on our own government to respect our country's constitution, its international obligations, and basic civic and human rights. Later, some of us were also sentenced to long prison terms in politically ordained judicial proceedings, just as the court in Beijing shamefully sentenced Mr. Liu Xiaobo in December 2009. <br />
<br />
We strongly believe, and we dare to remind you and your Government, that there is nothing subversive to state security when intellectuals, artists, writers and academics exercise their core vocation: to think, re-think, ask questions, criticize, act creatively, and try to initiate open dialogue. On the contrary, the present and future well-being of a society is undermined when governments suppress intellectual debate. <br />
<br />
There is nothing subversive to state security or damaging to future prosperity when citizens act guided by their own will and according to their best knowledge and conscience, when they associate among themselves to discuss and express peacefully their concerns and visions about the future development of their society. <br />
<br />
On the contrary, a country's material and spiritual future is undermined when its citizens are not allowed to act, associate, think and speak freely. <br />
<br />
This is why we call upon you and your Government to secure a fair and genuinely open trial for Liu Xiaobo when the court hears his appeal. <br />
<br />
We are also asking you and your Government to end the house arrests and police surveillance which have been imposed on other Charter 08 signatories. We call upon you and your Government to end the criminalization of free speech and to release all prisoners of conscience. <br />
<br />
Mr. President, we would like you to know that we will continue to watch carefully the treatment of Mr. Liu Xiaobo and other signatories of Charter 08. We will, together with many of our colleagues from the Czech Republic and Slovakia who signed the original Charter 77, make continued and sustained efforts to draw international attention to their plight. <br />
<br />
<br />
With regards, <br />
<br />
Vaclav Havel, playwright<br />
 <br />
Pavel Landovsky, autor<br />
 <br />
Vaclav Maly, Bishop of Prague</div>

							
							
							
							
