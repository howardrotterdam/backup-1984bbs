													<h2>尔冬升：审查制度让香港电影失去特征走向和谐</h2>
						
						
																											<div id="postmessage_51548" class="t_msgfont">尔冬升：3年后再也不见香港电影<br />
<br />
新闻晨报3月9日报道 “以前你们喜欢看的那种香港电影，以后可能就真没有了。像我本身喜欢看僵尸类型的喜剧，多好看，现在基本没有了”――尔冬升<br />
<br />
采访尔冬升是在电影《一路有你》的开机宴上，他连衣服领子都没有翻好，就坐下来闲话家常似的和记者侃侃而谈。虽然新片开拍，话题却绕不开那部他筹备了十年的《新宿事件》。电影刚被选中今年亚洲电影节开幕片，却因不能过审而无缘内地大银幕。尔冬升没有流露太多的失望，他说自己已经尽力，但保全一部好电影比保全票房更重要。<br />
<br />
当陈可辛、刘镇伟纷纷大张旗鼓北上时，尔冬升却说还想坚守香港，为香港电影站好最后一班岗。“再过三年，可能就没有真正的香港电影了”，尔冬升说自己保持一年一片的速度，有兴趣就带带年轻导演当监制，“没那么忙比较好，空下来我可以去潜水，在水下，你可以什么都不用想”。<br />
<br />
说《新宿事件》<br />
<br />
“通不过，也不能去糊弄人”<br />
<br />
晨报记者(以下简称晨)：听说《新宿事件》里的镜头非常暴力，所以没有通过内地审查？<br />
<br />
尔冬升(以下简称尔)：其实不是没过审，是根本就没送审，因为知道送了也不会过。我尝试过剪一个洁版，找了包括两个内地导演在内的5个业内人来看，看完都说不行，支离破碎得没法看。《新宿事件》里的确有很多打戏很暴力，但剪掉的话故事完全交代不清楚，比如说把第一场剪掉，观众就不明白吴彦祖为什么会被人打得那么惨，成龙为什么会去砍人？<br />
<br />
晨：影片不能在内地上映，会不会觉得很可惜？<br />
<br />
尔：没有办法，我知道影片在内地上一定会卖钱，但通不过也就算了，总不能去糊弄人。几年前，香港片刚一窝蜂进内地时，也是剪得乱七八糟，好比刘德华《大块头有大智慧》内地版完全看不懂，观众进去之后都翻脸了。这次放弃内地，成龙也跟我急，我就跟他说，如果我们只是为了卖票，乱剪一个版本骗观众进去，那名声坏掉了，以后拍什么都难了。<br />
<br />
晨：内地市场很大，对影片收回成本是不是有影响？<br />
<br />
尔：今年电影局很严格，合拍片一定要求内地、香港和国际都是一个版本。如果前面的戏都按照内地的要求改了，那国际市场怎么办？成龙的市场其实很大的，我们现在南美版权也卖掉了，要是上了中国内地这个版，可能欧美日本……全世界都完了。就这部影片来说，内地市场远远没有成龙的国际市场那么大。<br />
晨：所以，这个片子，内地观众没有办法看到了？<br />
<br />
尔：只是不能在电影院看到吧(笑)。这个戏4月4日在香港上，影片没过审，盗版商都开心死了，我听片方说都有盗版商找上门来谈合作。当然我们绝不会答应，那样对买票的观众太伤害了。<br />
<br />
晨：你的上一部电影《门徒》其实讲了很多制毒贩毒细节，当时内地审查就通过了？<br />
<br />
尔：《门徒》放完以后，好多香港导演都来找我，让我给意见怎样能过怎样不能过，真以为我是审剧本的专家。其实呢，我也不知道标准在哪里。《新宿事件》剧本是通过审查的，但拍完就过不了了。我有时候也想，现在合拍片，警察都是好人，坏蛋都被抓住，就好像其他导演也说，我们现在拍的戏不是真的生活，是理想中的生活。<br />
<br />
晨：内地严格的电影审查制度，有没有什么办法改变？<br />
<br />
尔：没有办法，我觉得这就是现状。其实现在新加坡和马来西亚比中国内地还严，他们直接在拷贝上动刀子，那边观众也习惯了，看到不连贯就知道这里剪了，那里剪了，反正我们导演也从不去那边看，随便他们怎么剪。中国内地的话，我听说将来还是要推分级制，分两级，那样可能会好一点。<br />
<br />
话香港电影<br />
<br />
“想拍《旺角黑夜》那样的电影”<br />
<br />
晨：你以后不会再拍《SQ男女》这样的片子了吧？<br />
<br />
尔：现在这个问题很头疼，这些题材特别的戏尽量去做，如果没有内地市场，投资的话欧洲找一点，日本找一点，这个过程会特别累。但拍完了不一定就不能放。香港现在还有些小片子，像叶念琛拍的爱情片，按照进口片在广东放就很受欢迎，他的片子如果送审可能也过不了，爱情关系那么复杂混乱，但拍完了观感可能就不同，剪一剪也就上了。<br />
<br />
晨：很多香港导演都开始北移，像徐克都在尝试拍内地题材的电影，但似乎不是很成功。<br />
<br />
尔：我觉得很难的。冯小刚那么受大家欢迎，因为他每天都上网，每天都生活在北京那个城市，这让他的触角会很敏锐。如果我不在北京生活，去拍内地题材的电影就一定要找人帮忙，有些感觉我是抓不到的。<br />
<br />
晨：你觉得在这种融合的环境下，纯粹的香港电影还会保留吗？<br />
<br />
尔：大概半年前，我还觉得内地市场太大，能包容很多电影，纯粹的港片还能坚持5年。《画皮》之后，我觉得香港特色的电影3年后就没有了。如果广东的院线建设得好，粤语片可能还会留下来，但以前你们喜欢看的那种香港电影，以后可能就真没有了。像我本身喜欢看僵尸类型的喜剧，多好看，现在基本没有了。<br />
<br />
晨：现在香港影坛好像也只有杜琪峰还在坚持？<br />
<br />
尔：香港也就他还在拗啊。<br />
<br />
晨：那你在什么位置呢，是像杜琪峰那样坚持，还是像陈可辛那样把公司开到内地？<br />
<br />
尔：还可以合作的时候，我就想还是走合拍的形式。我不想给人一个感觉，我要来这里，就插个旗，说我来啦。我的公司还在香港，制作和出品比较多，自己投资的不多。将来我拍的电影尊重内地市场是一定要的，但我也还想拍些《旺角黑夜》那样的电影，等放在比较空当找海外的投资来拍，也不用很多钱，因为不需要大牌演员，反正对外国市场来说，除了成龙和李连杰，谁演都一样。<br />
<br />
[回放]<br />
<br />
《新宿事件》无缘内地始末<br />
<br />
电影《新宿事件》是尔冬升筹备10年的心血之作，也是他答应帮成龙转型量身订做的电影。片中，成龙为寻觅在日本失去踪影的女友徐静蕾来到东京，结果他发现女友竟已下嫁日本黑帮大哥，意志消沉的他在遇见另一个从中国内地到日本的“妈妈桑”范冰冰后，生命又重燃斗志，但不幸卷入了日本黑帮的权力争斗中……<br />
<br />
全片在东京取景，去年年初杀青，原定国庆档期在内地和香港同步上映，但影片从去年7月开始就停止了所有的宣传。到9月底，香港媒体才爆出《新宿事件》可能在内地遭禁。10月初，一个名为“电检员”的网民在论坛爆料，称片中有不少暴力及血腥镜头，所以香港方面也不断讨论是否把该片列为III级片。《新宿事件》无缘内地，原因被指有三：其一是过分暴力血腥，片中吴彦祖的肠子被拖出体外，成龙最后浮尸东京湾也相当血腥；其二是题材敏感，影片主线讲述的是成龙的角色在新宿如何走上黑社会的故事；其三是涉嫌辱华，片中主角不是黑社会成员，就是妈妈桑，被指有辱海外华人的正面形象。<br />
<br />
