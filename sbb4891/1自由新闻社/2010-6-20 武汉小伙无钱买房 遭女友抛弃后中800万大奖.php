<br />
<img src="http://img.hexun.com/2010-06-20/124007285.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
&nbsp; &nbsp;&nbsp; &nbsp; 这就是那张被揣得皱巴巴的中奖彩票。（图片来源:荆楚网）<br />
<br />
　　因买不起婚房,无奈与相恋10年的女友分手;谁知一个月后,竟中得817万元福彩大奖,如今武汉小伙小胡手握巨款,只盼女友能回心转意。<br />
　　6月8日,“双色球”第10065期开奖,我省（湖北）武汉一彩民采用10元单式票中得1注一等奖,单注奖金高达817万元。前日,中奖者小胡前往省福彩中心兑领了该注大奖,引出了一段传奇故事。<br />
<br />
　　中奖一周不知情<br />
　　大奖彩票险被洗<br />
<br />
　　30岁出头的小胡是土生土长的武汉伢,父母靠做小生意谋生。打工近十年的他做梦也没想到,自己会中大奖,而且一中就是800多万元。更让人称奇的是,他中大奖后一周才知道自己中了奖,那张价值800多万元的彩票,则被他胡乱揣在牛仔裤的裤兜里,换洗时险些被妈妈洗掉。<br />
<br />
　　据了解,小胡在中奖一周后,再度去买彩票时才知道中了大奖。“我只要有空,就会去前进四路那个站点机选10元钱的彩票,上周买了彩票后,一直蛮忙,直到一星期后,我才带上彩票前往站点兑奖,并准备继续机选10元。”面对记者,小胡倒是一脸平静,“其实,中奖第二天我就知道那个站点出了大奖,但我买彩多年,只中过小奖,所以当时并没非分之想。”<br />
　　顿了顿,小胡又说:“直到拿出彩票核对过后,我才明白发生了什么事。但片刻的慌乱和狂喜过后,我仍然故作镇定地机选了几注彩票,才迅速离开站点,并在第一时间将喜讯告诉了买彩十多年却一无所获的爸爸。”<br />
　　随后,父子俩一起打车去了福彩中心,却碰上端午节放假。“从那以后,我和老伴就没平静过。我们把中奖彩票锁起来不说,还天天守着它,连觉都睡不安稳。”老胡笑着说。<br />
<br />
　　因买不起房刚失恋<br />
　　中奖后仍盼佳人归<br />
<br />
　　接受采访时,小胡脸上浮出一丝忧郁。细问之下,记者才知道,他和相恋了10年的女友一个月前刚分手。“本来是准备今年结婚的。唉!”小胡又自嘲地笑了笑。<br />
<br />
　　据小胡介绍,他与女友分手,原因就是买不起婚房。小胡的父母也在一旁责怪儿子:“多好的一个女伢啊,如今上哪里去找这么真心对你好的人。”“还不晚嘛……”一旁的工作人员纷纷插嘴,“现在房子不是问题了,一辈子能有几个真心陪你十年的人。珍惜吧。”小胡像是有所触动,不过,又似乎面露难色。“这样吧,你回去后就和她说,你就说我们请她来吃饭。”当妈的似乎看透了小胡的心思。“这些时,我们心里哪里轻松哟。”小胡的妈妈叹了口气:先是为儿子的婚事着急,中了奖后,还是急。不过,现在是急这钱如何安排。“钱是好东西,可把握不好,也有可能会害人的。”老胡也在一边语重心长地和儿子唠叨着。“你就告诉她中大奖了,不就结了!”一旁有人打趣道。“那可不行!”老胡两口子几乎异口同声:靠钱上哪里找不到个媳妇!关键是要真心对你好的。打趣者连称:开玩笑的,开玩笑的。<br />
<br />
　　不过,此话倒是惹得小胡心事重重:我是得去找她好好谈谈了。不管怎样,我们一起走过了十年,我总得给她一个重新选择的机会,也给我自己一个机会。<br />
