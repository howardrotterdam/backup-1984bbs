韩海鸥经办的“大连友谊”是个什么生意？ <br />
<br />
在大连，大连友谊(集团)股份有限公司是一家知名企业，很多人都知道它与薄熙来的关系非同寻常。它于1993年，也就是在薄熙来刚当上副市长，市长之时，经市经济体制改革委员会大体改委发[1993]76号，《关于设立大连友谊(集团)股份有限公司的批复》文件批准，,以定向募集方式设立的股份有限公司。1996年12月18日经中国证券监督管理委员会证监发字(1996)395号文批准,公司向社会公开发行人民币普通股3,500万股,由定向募集公司转为社会募集公司,经过历年分配与转增,现股本总额为23,760万股,其中已流通的人民币普通股14,904万股。公司注册资本为23760万元，截至2007年底，总资产46.96亿元，净资产8.28亿元，利润总额7280万元。<br />
<br />
大连新闻界人士称，谷开来律师很早就把这家企业常年法律谘询顾问生意拿到了手。大连另一资深律师尹某某对我报怨说，不是主动揽，是人家上门送的，我爹是薄一波，我当市长，我太太开律师所，肯定不会没有客户！<br />
<br />
当然，薄熙来通过手中的权力，大搞企业股份制改革，做为市政府一商局下属企业的友谊集团，的确是受益了，在其离开辽宁省之后，他仍然强有力地支持这家公司。<br />
<br />
2006年7月18日，这家公司实施了股权分置改革，公司非流通股股东大连友谊集团有限公司，为获得流通权向A股流通股股东执行对价安排，A股流通股股东每持10股A股获付2.7股及2元人民币的执行对价，对价所付出的股份总数为29,160,000股。至此公司已流通的人民币普通股为13,716万股。自此又上一个新台阶。凭藉公司多年积累的经营管理实力，良好的公众形象，在“真诚相待，追求完美”企业管理理念指导下，公司以效益最大化为发展核心，实施产业投资发展坚持有进有退，长短结合，做强主业，优势互补，资源共享，多元发展的长期发展战略，不断提升公司的核心竞争力，为股东创造了巨大的财富<br />
<br />
回顾过去，大连友谊集团真是天壤之变。它始建于1958年，1993年组建大连友谊（集团）股份有限公司，2005年集团完成股份制改革，组建成为投资主体多元化的公司制企业。友谊集团拥有全资子公司、控股公司、分公司、参股公司32个，托管企业16个。在册员工2000余人，从业人员12000余人。<br />
<br />
50年以来，公司已由单一的外轮服务企业，发展成为了综合性的商业企业集团。近年来，它步入持续、稳步、快速的发展轨道，形成以零售业、酒店业、房地产业为主营业务，以进出口贸易、免税商品供应、船舶供应等策略性投资为补充的经营体系。其零售业以友谊商城为代表，其高级精品百货率先占据大连的高端消费市场，被国家商务部授予“金鼎百货精品店”的荣誉称号，也是东北地区首家获此殊荣的百货精品店。<br />
<br />
此外，其酒店业以富丽华大酒店为代表，是东北地区历史最为悠久的五星级酒店，以其优良的服务标准、精美的硬件设施、细致的人性化服务，荣获全球酒店论坛“五星金钻奖・亚洲十大品牌酒店”并位居榜首。从2007年开始，富丽华大酒店承接了“大连夏季达沃斯论坛”的接待工作，其完美的服务得到世界各地经济精英和政要的好评。<br />
<br />
其集团田老板为了报恩，也为了巴结薄谷夫妇两人，给他们在此专门预留了“行宫”，90年代后期日本首相桥本龙太郎来大连期间，我陪同采访，曾亲眼看见薄熙来经常晚上住在这家酒店，来来往往，目不旁视，显得神秘兮兮。有时田老板跑前跑后，一脸阿谀奉承的神态。<br />
<br />
我通过采访了解到，这个集团的房地产业以大连友谊合升公司、江苏友谊合升公司为代表。自2002年进入房地产业以来，坚持开发精品、创建品牌的战略，先后在大连和苏州地区开建了壹品星海、壹品漫谷项目和海尚壹品项目，在业界赢得良好的形象与口碑。而航运服务业，以友谊外轮供应公司为代表，是友谊的传统产业。近年来，外轮供应公司抓住振兴东北老工业基地和大连建设东北亚航运服务中心的历史机遇，加快发展航运服务业务，与中免总公司合作组建中免友谊外供免税店，建设大窑湾港区1.7万平方米的航运服务基地，成为国内目前具有一定规模的、为数不多的外供企业。<br />
<br />
这家位于大连市中山区七一街1号的企业，我十分熟悉，它历任老总我接触了不少，有许多人与我同感：一个领导干部因为有功于某企业，就可以让太太承办它所给的生意发财吗？<br />
<br />
我知道，大连友谊集团的法人代表是田某群，而他原为大连市政府的处级干部，是薄熙来亲自安排他走马上任的，当然，这是一个名利双收的“肥缺”。田处长亦官亦商，年薪上百万元，怎能不对薄熙来投李报挑呢？<br />
<br />
大连友谊集团股份有限公司的公开网上资料显示：田某群，1955年出生，男，研究生，1995年6月至今任大连富丽华大酒店董事长，2002年6月至今任大连友谊集团有限公司董事长，2003年5月至今任大连友谊(集团)股份有限公司董事，2005年7 月至今任大连友谊(集团)股份有限公司董事长。我印象中，当时原富丽华酒店老板张凤桥病逝后，有众多人看好了这个位置，薄熙来富有远见，一个令选中了田某群。这个“远见”是什么？大连一家媒体记者对我说，是把大生意给他太太做吧。<br />
<br />
从90年代中期的开来律师所，到现在的昂道所，薄熙来一家人与田老板一直关系过于密切，谷开来明目张胆地参与了这家股份制企业的法律事务，她到底得到了多少法律谘询费，向来讳莫如深。薄熙来的原秘书，现国安局党委书记车克民称其为“国家机秘”，谁也碰不得！<br />
<br />
大连一位知情者说，友谊（集团）股份有限公司，每年利润总额21,848万元。为田老板当律师，还能缺钱花？估计，光该集团给她的这一笔生意，谷开来就够一年支付部份办公费用了。<br />
<br />
专给大连上市公司搞法律谘询收入丰厚 <br />
<br />
类似的大连企业还有许多家，据一位曾在该所工作过的律师透露，别的律师所往往揽不到生意，这里却要每天拚命干，都做不完。最鼎盛时期，担任了100多家企业的常年法律顾问，还打赢了上百起民事官司，营业额多达1个亿，盈利9000万元。连当时大连最能干的著名律师苗松，陈德惠也自叹不如。<br />
