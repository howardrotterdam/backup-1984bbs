经本所律师审查，截止本法律意见书出具之日，星港公司转为非外商投资企业的手续正在办理之中。根据东北药、东药集团出具的《关于东北制药集团股份有限公司、东北制药集团公司和东北制药总厂之间关系的说明》，星港公司的全部股权实质上为东药集团所有，星港公司应注销。东北药已承诺本次关联交易后，将办理星港公司的注销手续，所有债权债务由东北药承担并转为东北药的分公司，因此星港公司2001年年检未进行。<br />
