生意跟着官运走<br />
<br />
2001年初，薄熙来买通江泽民与国安部特工，下令将我关进了监狱，加快了贪污受贿的步伐，更加肆无忌惮地招揽所谓律师业务。俗话讲，兔子不吃窝边草，但追逐金钱的欲望已使薄熙来与谷开来昏了头。2002年，昂道律师所又靠上了东北制药集团股份有限公司这棵大树。辽宁新闻界消息人士说，这叫“生意跟着官运走，走到哪里钱都有。”<br />
<br />
“东北制药”位于沈阳，既是在辽宁省薄熙来省长眼皮底下的“大肥肉”，它是以化学原料药为主，兼有生物发酵、中西药制剂和微生态制剂的大型综合性制药企业。其成立于1993年6月，是沈阳市首批试点股份制企业之一。2003年3月，原东北制药集团公司完成了债转股工作，发起成立了东北制药集团有限责任公司，由四家股东组成，有从业人员8900余人，年销售收入50多亿元，年出口创汇近2亿美元。东药集团是中国500强企业之一，是国家首批55家试点企业集团。2004年，“东北药”被瑞士达沃斯世界经济论坛和世界品牌实验室评为中国500最具价值品牌之一；2005年又被世界品牌实验室评为中国医药类十大品牌，并以价值26.13亿元位居2006年《中国500最具价值品牌》排行榜第247位。2006年1月23日,东北制药作为沈阳市首家股权分置改革的试点企业，,成功地进行了股权分置改革。此外，“东北”牌，被薄熙来任职当权的国家商务部，确定为2005-2006年度重点培育和发展的出口品牌。企业还被评为国家级“连续20年守合同重信用企业”。<br />
