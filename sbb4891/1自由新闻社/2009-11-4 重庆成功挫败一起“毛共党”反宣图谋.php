1、 东北制药集团公司拥有的制剂公司的全部资产和东北制药集团公司拥有的星港公司的48.92％的股权。<br />
<br />
制剂公司不具备独立法人资格，其资产的占有方为东药集团。经本所律师查证，东药集团拥有的制剂公司资产产权明晰，制剂公司资产不存在债权债务纠纷，不存在抵押等限制性法律关系存在。根据2002年4月25日北京龙源智博资产评估有限责任公司出具的龙源智博评报字[2002]C－018号评估报告，上述资产和股权的评估价值为29,959.29万元,双方协议作价29,241万元。<br />
<br />
2、 东北制药总厂（本部）持有的星港公司的51.08％的股权经律师查证，星港公司系经沈阳市人民政府外经贸沈府资字（1991）0137号批准证书批准，于1992年1月17日设立的中外合资企业，股东为东药集团和香港卫盛发展有限公司。2001年3月2日经沈阳市对外经济贸易委员会批准香港卫盛发展有限公司将其持有的51.08％的股权转让给东北制药总厂，变更为非外商投资企业。变更后东药集团持有星港公司48.92％的股权，东北制药总厂（本部）持有星港公司51.08％的股权。<br />
<br />
根据东北药、东药集团出具的《关于东北制药集团股份有限公司、东北制药集团公司和东北制药总厂之间关系的说明》，东北制药总厂（本部）持有的星港公司的股份属于东药集团所有，在财务上体现为东药集团的对外投资，实质上星港公司全部股权为东药集团所有，应办理注销登记手续，东北药已承诺本次关联交易后办理相关手续。根据2002年4月25日北京龙源智博资产评估有限责任公司出具的龙源智博评报字[2002]C－019号评估报告，星港公司的净资产评估价值为4,330.94万元，其中东北制药总厂（本部）持有的星港公司的51.08％股权评估价值为2,212.24万元。经核查，本次东药集团转让其持有星港公司48.92％的股权和东北制药总厂（本部）转让其持有的星港公司的51.08％的股权，不存在债权债务纠纷，不存在质押等限制性法律关系存在。<br />
