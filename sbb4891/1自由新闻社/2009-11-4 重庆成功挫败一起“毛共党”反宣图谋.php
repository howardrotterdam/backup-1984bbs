80年代中期，律师业悄然兴起，但大连地处沿海小城，490万人口中，对律师业有清晰认识的人还不多，所以时常有律师找到我，向我苦诉揽活难，律师界风气不正，有的人给予中介人高价回扣，有的与法官暗渡陈仓，为了生存，千方百计，根本没有道德底线，可见生意难做，然而谷开来一挂出“开来律师事务所”的牌子，立即顾客盈门，有些大生意推不出去门。一位大连司法界权威人士愤愤不平地对我说，假如她在深圳开办律师事务所，不在大连当地经营，这问题不大，但她在自已老公当权的小城市大连就地开业，会形成怎样一种不公平的局面呢？一方面是法院对她承办的民事诉讼案件有理亦让三分，谁都怕薄大人公报私仇，另一方面是财大气粗的企业，无一不希望她做为自家的常年法律顾问，以便藉机向市长进贡，做感情投资。这样名正言顺，也不违法。但实质上这是变相的行贿受贿。<br />
<br />
在大连市中山区青泥洼桥的百丽大厦，即现在的“大商男店”原址，有谷开来租用的写字间，分别位于两个楼层6与7，其一度还有与美籍华人，大连荣誉市民程某君合搞的所谓“大连惠瑞斯顾问投资有限公司”，可见规摸不小，气派不凡。由于当时正巧我的一个亲友，在其楼上的某服装贸易公司工作，所以我对该所生意兴隆情况，多有耳闻。<br />
<br />
1998年，香港文汇报广州办事处调来一个新同事，与我满谈得来，她姓林，原为中国包装报副总编，现任广办副主任。有一次在广州开会闲聊，她称与谷开来为北大同学，在同个寝室住过，关系不错，故我回大连前她嘱我去代为问安，并修书一封，但几天后当我去拜访谷的律师事务所时，谷竟让一个叫赵丹的秘书接待我，并不在意林某某的室友之情，我只好留下书信，她也不做回覆。可见其心地冷漠与失礼。而且还发生以下兴师问罪的突发怪事。<br />
<br />
当日午后，当我驾车离开百丽大厦之后不久，我还在外面公干，市政府查办处处长宋某某便带领三四个人，急匆勿赶到大连沙河口区民权街348号天河公寓18搂，即文汇报东北办，值班小姐称，宋某某出示了工作证，问我在不在，去开来律师所干嘛，态度很凶很傲慢。后来我想了一下，她的公司肯定存在相当严重的经济问题，只有心虚胆怯才敢派市政府查办处的人跑过来恐吓我。而能够调动市政府查办处的人，只有薄熙来，这件事不单没有吓倒我，反倒激起了我揭露这家公司内幕的极大兴趣。<br />
<br />
另有多人告诉我，谷开来这个公司原挂牌在大连开发区，现在业务发展过太快，才迁到了大连，并已在北京，香港以至美国纽约都有据点，势力雄厚，行动诡秘，人才众多，生意很好，在大连影响力日见强大。1999年大连大学教授，著名书法家于植元对我说，薄熙来当市长，老婆开律师事务所，大家都很有意见，原司法部长蔡城视察大连时，对他说，他们夫妻这样搞，大连还能有法律吗？<br />
<br />
律师所改名是旧瓶装新酒 <br />
<br />
近些年，由于香港《前哨》杂志等媒体的揭露与报导，北京开来律师事务所的名声不佳，特别是薄熙来与谷开来担心，原先印在名片上的香港与纽约的分所地址，会使中纪委顺藤摸瓜，追查他们的海外资产。所以改名为北京市昂道律师事务所，但旧瓶装新酒，换汤不换药。其公开刊登在网络上的信息称，该所是由著名大律师谷开来女士创办的，借鉴英美律师事务所的操作方法，结合中国实际情况而建立的综合性律师事务所，主要处理国内和中外经济、文化往来中涉及到的各种法律业务。客户分布在美、英、加、日、韩、新、港、台和欧洲少数国家及国内部份省市。<br />
<br />
《简介》还表示，该律师事务所的人员由执业律师、顾问、行政人员及助理人员组成，现有专职律师28名，文秘行政人员10名。总部设在北京，已在大连市和郑州市分别设立了分所。该律师事务所在处理业务上实行主任律师、专职律师、律师助理综合配置，采取集体作业、分工协作的形式为客户提供优质的法律服务。<br />
<br />
虽然，谷开来是大老板，但赵东平(律师，北京大学法律系毕业)却是撑门户的人，近期刊发在《青年周末》上的薄熙来与谷开来的儿子薄瓜瓜的文章，则透露了他妈妈曾经常陪同他在国外居住的消息，这表明她有足够的银子雇佣大批人员。<br />
<br />
上述文字显示，昂道律师所光是在北京总部的人，即有38人之多，其中28个律师，按每人平均月收入2万元计算工资，其支出便要672万，再加上10个文秘行政人员，按每人月薪5000元计，即需60万，共需要732万。由此可想而知，其盈利大约每年至少多达1000万，才能维持生存。那么，在目前经济不景气的情况下，昂道所都开展了哪些正常律师业务呢？<br />
<br />
据知情人透露，北京昂道律师所提供法律服务的主要形式有：常年法律顾问；专项委托法律顾问；非诉案件及非诉法律事务的代理；仲裁与诉讼案件的代理。其主要业务分类为：案情分析；中国及海外投资项目法律业务；中国公司在境外投资或合作项目的法律业务；公司法律业务；房地产法律业务；金融财税法律业务；证券、期货、上市公司法律业务；知识权、科学技术法律业务；海事仲裁、国际法律业务；诉讼、仲裁法律业务。其专长领域为：暴力伤害，仲裁，证券，医疗事故，反倾销等。其联系地址：北京市朝阳区亚运村汇园公寓K座9门318室。然而，真人不露相。过去，在大连时是美籍华人程某君当代理人，现在是主任律师赵东平出面接待，还把他的手机号码电话13901130228也刊登在网上。律师所的办公电话：010―64992318传真：010―64992405。<br />
<br />
我细心地注意到了上述几个号码大都带“8”：318室，手机228，坐机318，其在大连的一处公寓是：大连市西岗区长江路598号，28楼，等等。由此可见，薄熙来与太太谷开来满脑子想的都是钱，都是“发”“发”“发”。<br />
