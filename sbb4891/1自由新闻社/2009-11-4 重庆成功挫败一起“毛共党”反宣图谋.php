在大连设分所的时间为何谎称1999年？ <br />
<br />
其实，谷开来律师事务所在大连，是伴随着薄熙来的权势而生长发展的，从大连到沈阳以至北京，莫不如此，最初为了掩人耳目，先声称总部设在北京，后在1999年6月又声称在大连才刚建分所，并把这一信息公布在网上，企图浑淆视听，一笔抹去往昔的罪恶。这是因为，薄熙来藉助江泽民的支持，正由大连市长爬上了辽宁省长的宝座，但大连的市委书记于学祥，与省委书记闻世震等人，却在里应外合，阻挡他于中共辽宁省第9届党代会上，他没有当上省委书记。他知道，自己与太太谷开来以开办律师事务所为招牌，大肆索贿受贿，积聚了巨额非法所得，早已被这些人盯住，所以必须小心。一方面他们把两个孩子全部送到国外读书，一个在英国，一个在美国，谷开来还得到了新加坡等国的“绿卡”，薄熙来成了名符其实的“裸官”，另一方面仍然在大连，通过当政10多年形成的社会各界关系网，开展所谓律师业务，大搞权钱交易，大举捞取钱财，以满足其家人跨国界的奢华生活。<br />
<br />
据大连新闻界消息灵通人士披露，北京市昂道律师事务所，在大连也笼络了大批人才，其声称大连分所业务服务方式与理念是，靠团队精神攻坚，靠团队精神取胜。然而，真正靠的还是薄熙来这个当官的手中权势攒钱。其拥有所谓天某网律师韩某鸥，王某勇，李某华，刁某，万某，佟某玲，王某，梁某鹏，高某，李某白等多人。仿佛连这个在大连仅次于大连日报的新闻网络，也是薄熙来与谷开来夫妇两人自家创办的。年轻有为的律师们正在环绕着薄熙来的光环，奔钱而去。<br />
<br />
接近昂道律师事务所大连分所的消息人士说，其位于大连市中山区人民路23号虹源大厦1903室的办公室，终日生意兴隆，财源滚滚。<br />
