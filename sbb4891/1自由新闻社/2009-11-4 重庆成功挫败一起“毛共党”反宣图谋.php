北京市昂道律师事务所经办律师：李哲<br />
二○○四年九月二十八日<br />
<br />
长春新闻界消息灵通人士对我说，长春百货不论亏不亏钱，巨额的律师费还是要拿的，估计不会低于一百万元，假如薄熙来不是商务部长，也罢了，他是堂堂的“商界一哥”啊，太太又拿走了东北商业口的一块大“肥肉”。你说这是律师费还是贿赂款？这是正常的生意，还是公开的敲诈勒索？我听了，只能一声叹息！<br />
<br />
为什么中纪委抓商务部贪腐案虎头蛇尾？ <br />
<br />
据去年9月2日《21世纪经济》报导，商务部条法司巡视员郭京毅8月13日被中纪委“双规”。由于纪检监察部门此前已经掌握了大量情况，因此在8月13日带走郭京毅的同时，由检察机关同时逮捕了涉案的另外两名律师――来自北京思风峰律师事务所的张玉栋和曾在那里工作过的刘阳。我认为，这个案件涉及高层权斗，是胡锦涛的共青团派针对薄熙来发起的进攻，它不仅阻断了薄熙来进军副总理之路，而且逼迫其归顺自己，灰溜溜地去了重庆。<br />
<br />
报导称，此次行动显然事先经过了周密策划和安排。目前该案仍然处于高度保密状态，中纪委驻商务部纪检组、监察部驻商务部监察局表示，关于此案“无可奉告”。但是知情人士指出，此三人涉及的案件相信与一家外资公司的并购项目行贿受贿有关。同时，此案可能还会涉及到其它政府部门的官员。显然，这是商务部第一大案，薄熙来难脱其咎。<br />
<br />
媒体称，郭京毅是商务部条法司正司级巡视员，他也因此成为商务部2003年设立以来，落马的最高级别官员。此前，他仕途平顺。1982年至1986年，他就读于北京大学法律系国际法专业，毕业后即到外经贸部条法司工作，曾在投资法律处工作多年，历任副处长、处长、副司长。2003年外经贸部并入商务部之后，郭京毅仍然担任条法司副司长，并于2007年3月被薄熙来亲自提升为正司级巡视员。在商务部司级干部中，44岁的郭京毅年轻有为，颇有前程。薄熙来还让郭京毅兼任商务部反垄断办公室副主任。近年来出台和修订的一系列重要投资法律，如《关于外国投资者并购境内企业的规定》、《关于外商投资的公司审批登记管理法律适用若干问题的执行意见》皆与郭有关。至于郭所在的商务部条法司的职责，业内人士介绍，条法司的职责是法规的制定和法律适用标准的释明，也就是说，条法司制定的法规，如何适用，要请条法司来解释。事实上，这种适用的解释，权限是相当大的。在商务部内部，一个重要的事项决策，需要几个部门一起会签，条法司肯定是其中重要部门之一。以外资并购为例，有几项肯定要去条法司审批，例如是否符合外资产业指导目录，还有根据十号文的规定，外资合并，金额在1个亿以上的，要去商务部批等。此外，投资公司中，境内出去的和境外进来的，这些都要条法司参与会签。但不论怎样，都必须由商务部长薄熙来最后签字。<br />
<br />
耐人寻味的是，这回中纪委还抓捕了两个律师，而这些人都是谷开来的北大同系学弟，并且毕业后是吃同一碗饭的。媒体报导说，郭京毅被“双规”与律师张玉栋直接相关。张玉栋是郭京毅在北大的同班同学，两人曾经在同一间宿舍住了四年，交情不错。毕业后，郭京毅进入外经贸司，而张玉栋则做了律师，专门从事外国投资项目。担任了北京思峰律师事务所主任。他的客户包括中国银行、长江集团、爱立信、和记、现代汽车、柯达、微软、摩托罗拉、松下、尼桑汽车、诺基亚、壳牌等众多外企。可见生意相当不错。但北京新闻界的朋友对我说，该律师事务所最旺时也拥有不到10名合伙人，与谷开来的“昂道”根本没法相比，在中国，总共两百余家外资投资性公司中，它能为其中超过半数投资性公司的设立及日常业务提供法律服务，而谷开来的本事可想而知，她有20个合伙人，是张律师的一倍，而且还有薄熙来这个大后台揽生意。这就是中纪委既抓官员又抓律师的真实原因。<br />
<br />
媒体报导说，由于工作因素，毕业后的张玉栋和郭京毅仍然来往密切。一位他们当年的同学向记者表示，该班毕业后大部份同学尽管都在外商企业或者从事涉外法律工作，但没有人胜过他，因为张玉栋过去几年一直与外经贸部、工商局、计委、经贸委、税务总局、各商业银行总行及某些地方政府机关保持着极为密切的关系。他还与许多在英国、美国和香港的专业机构保持着良好的交往。知情人士透露，案件可能涉及到了境外的律师行。案情或涉外资项目审批案件中的另一关键人物，是曾经供职于思峰所的刘阳。他于1995年毕业于对外经贸大学国际经济法专业后，进入外经贸部投资法律司，曾经参与起草了自1995年以后中国许多重大的利用外资方面的法律、法规，其中包括《关于设立外商投资股份有限公司若干问题的暂行规定》，《中外合资经营企业法》和《中外合作经营企业法》等。2003年底，他从商务部辞职，进入思峰所，2008年3月，他离开思峰，又成立了北京善信律师事务所。目前，这两家律师事务所都对他被捕一事讳莫如深。一年过去了，至今法院亦未宣判。<br />
<br />
然而，据北京与大连新闻界消息灵通人士称，中纪委这一举措把薄熙来夫妇吓坏了，他们马上找江泽民等太子党分子，通过各种方式给胡温与中纪委施压，结果造成此案虎头蛇尾。<br />
<br />
另据媒体报导，原薄一波秘书，国家开发银行副行长王益于今年6月8日晚被正式“双规”，事涉多宗证监案。这又给薄熙来以沉重的打击。报导说，王益在6月8日端午节当天，赴宁波参加博鳌现代物流与自由港国际论坛并发言。在当晚由栎社机场搭机返京途中，他在乘坐的头等舱里受到办案人员监控。飞机抵达北京后，办案人员随王益至其办公室，正式执行“双规”。今年52岁的王益曾在证券监管机构任职多年。他1992年至1995年任国务院证券委办公室副主任，1995年11月至1999年2月任中国证监会副主席，分管股票发行、基金等重要部门。王益早年毕业于北京大学历史系，在任薄一波的秘书时与薄家，特别是薄熙来结下深厚友情。大连许多企业改股上市，便是当年通过薄熙来求助于王益办成的。假如深挖下去，他们都是一根草叶上的蚂蚱。然而，与郭京毅案一样，王益案至今亦未由法院宣布判决。这到底是怎么回事呢？<br />
<br />
由上述事实，已足以证明，不论是在大连当市长，沈阳当省长期间，还是在北京当国家商务部部长时，薄熙来都有以权媒私的经济犯罪行为。特别是当省长一年半之后，就做成了“东北制药”的生意，当了部长之后才9个月，又迫不急待地拿下了“长春百货”的业务大单。至于那些我们目前还不知道的所谓律师生意，一定会更多！更触目惊心！其罪证如山，岂用质疑？！胡温与中纪委为什么只打苍蝇，不打老虎？为什么只泛泛地空讲廉政建设，却不敢对坐在主席台上的中共太子党薄熙来贪腐案开刀呢？眼下，他还在重庆抓捕文强等小贪官，大造声势，这怎么能够服众？因此我要猛喝一声：千万别被他所谓“反贪打黑”的闹剧转移了人民的视线，胡锦涛，温家宝，要拿出一点勇气来，立即抓住他！他才是真正的大硕鼠！机不可失，时不再来！<br />
<br />
