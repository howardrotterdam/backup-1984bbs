																											<div id="postmessage_401480" class="t_msgfont">河南曹操墓发掘大揭秘：多亏了那些盗墓贼&nbsp;&nbsp;<br />
<br />
&nbsp;&nbsp;作者:新在水一方 于 2009-12-28&nbsp;&nbsp;<br />
&nbsp;&nbsp;<br />
<br />
【侨报网讯】27日，曹操墓文物发掘工程总负责人、西高穴东汉大墓考古队队长、河南省文物局文物考古研究所副调研员潘伟斌，接受记者专访，细谈发掘的前前后后。<br />
<br />
　　直接证据 魏武王三个字<br />
<br />
　　记者(以下简称记)：曹操墓终于被认定了！<br />
<br />
　　潘伟斌(以下简称潘)：是啊！我现在在北京，手机快被打爆了，都是预约采访。<br />
<br />
　　记：此次新闻发布会为何在北京？<br />
<br />
　　潘：是国家文物局要求在北京开。<br />
<br />
　　<br />
记：从曹操墓里都挖出了哪些文物？<br />
<br />
　　潘：墓被盗得很严重，但仍幸存一些重要随葬品。共出土器物250余件，以刻铭石牌最为重要。刻铭石牌共出土59件，有长方形、圭形等，铭文记录了随葬物品的名称和数量。<br />
<br />
　　除上述器物外，该墓还出土有大量画像石残块。这批画像石画工精细娴熟，雕刻精美，内容丰富，有“神兽”、“七女复仇”等图案，并刻有“咸阳令”、“纪梁”、“侍郎”、等文字，堪称汉画像石中不可多得的精品。<br />
<br />
　　记：是哪些关键证据让专家们下了结论？<br />
<br />
　　潘：圭和璧是帝王陵墓的直接证据，魏武王三个字也是直接证据。其中8件圭形石牌极为珍贵，分别刻有“魏武王常所用虎大戟”、“魏武王常所用虎大刀” 等铭文。在一件石枕上，刻有“魏武王常所用慰项石”铭文。这些出土文字材料为研究确定墓主身份提供了重要的、最直接的历史学依据。<br />
<br />
　　记：现在这些文物都在哪里呢？<br />
<br />
　　潘：安阳县安丰乡西高穴村考古工地。<br />
<br />
　　记：现在这个工地的挖掘工程结束了吗？<br />
<br />
　　潘：还没有。<br />
<br />
　　论证过程 结合文献多次论证<br />
<br />
　　记：进行论证的专家来自哪里？<br />
<br />
　　潘：有社科院考古所的、北大的、国家文物局的等。<br />
<br />
　　记：论证过程怎样？<br />
<br />
　　潘：专家对上述考古发现进行了多次论证，根据墓葬形制、结构及随葬品特征，认为这座大墓年代为东汉晚期，结合文献记载，判定该墓的墓主人为曹操，大墓即文献中记载的高陵。<br />
<br />
　　记：曹操墓有什么特别之处？<br />
<br />
　　潘：这座墓葬规模巨大，总长度近60米，墓室结构与曹操魏王的身份相称;该墓未发现封土，也与文献记载曹操寿陵“因高为基，不封不树”的情况相符合。<br />
<br />
　　记：什么文献呢？<br />
<br />
　　潘：如《三国志・魏书・武帝纪》载：“庚子，王崩于洛阳，年六十，谥曰武王，二月丁卯，葬高陵。”又说：“建安二十三年(公元218年)六月令曰：古之葬者，必居瘠薄之地。其规西原上为寿陵，因高为基，不封不树。”<br />
<br />
　　记：文献中的西门豹祠就是曹操墓位置？<br />
<br />
　　潘：西门豹祠地属河南省安阳县丰乐镇。这里有一高地，其上为一南北朝时期的遗址。地面上散落着不少东汉、东魏、北齐时的砖瓦残片，说明在东汉、东魏、北齐时，这里曾有地面建筑，比如庙宇宫殿等。<br />
<br />
　　但凭这些还不足于断定曹操墓的位置。西高穴村村民徐玉超曾经挖出了后赵建武十一年(公元345年)大仆卿驸马都尉鲁潜墓志，上面说：&quot;故魏武帝陵西北角西行四十三步&quot;。它首次明确记载了曹操墓的具体方位，于是我们才锁定在漳河南岸的西高穴村范围内。<br />
<br />
　　记：现在挖出的曹操墓与文献记载还有哪些相符？<br />
<br />
　　潘：文献明确记载，曹操主张薄葬，《遗令》：“殓以时服”、“无藏金玉珍宝”，也在墓葬中得到了印证。<br />
<br />
　　防人盗掘 先后抓了5批盗墓贼<br />
<br />
　　记：有人告诉我，直接证据是从盗墓贼手里拿到的？<br />
<br />
　　潘：是的。2008年，盗墓贼从二号墓中盗出画像石一块，其画面反映的就是东汉时期常见的八女投江图，从铭文看这绝不是一般平民墓葬或官僚墓葬。同时，从此墓葬中盗出铭牌一块，其上面刻有“魏<br />
武王常所用虎大刀”字样。<br />
<br />
　　记：盗墓贼是怎么抓到的呢？<br />
<br />
　　潘：是我们对文物作鉴定和到现场调查时发现的，可以参访当地派出所。此墓葬从06年开始盗掘，被盗得很严重。当地派出所先后抓了5批盗墓贼，38人。派出所抓到人后，盗墓贼手中的东西是我们发现曹操墓的直接证据。<br />
<br />
　　记：您有没有见到过那些盗墓贼呢？他们一般都是什么人？<br />
<br />
　　潘：当然见到过，都是周围村庄的农民，也有外地的。我们要将这些文物进行修复整理。我们在之前也不知道这就是曹操墓，只是后来发现了，才知道的。<br />
<br />
　　记：就是根据盗墓贼手中的东西找到了曹操墓？<br />
<br />
　　潘：是的。位置也随之确定。一年前，经过国家文物局的批准，由安阳市政府出考古经费，此项挖掘工程开始了。当时还不敢说是曹操墓，就成立了“西高穴东汉大墓”考古队，一共是20多个人。后来由于人手不够，安阳市、安阳县的考古人员陆陆续续补充进去。再后来随着发掘工程的深入，安阳市成立了“西高穴大墓保护领导小组”&quot;，安阳市市长任组长，主要是为了发现曹操墓之后加强管理，包括保护、展示、规划、开发等。<br />
<br />
　　记：这一年的发掘工作，是不是比较辛苦？<br />
<br />
　　潘：是啊！我记得2008年12曰6日，我们河南省考古研究所的人来到西高穴村，抱着被子，拿着日用品住了下来。住在村东头的一个大院里，这个院子是租一个村民的。这一年来，我很少回郑州的家里完完整整度过一个周末，特别是今年年末，每天都很忙很累。在这一年的挖掘中，知道的人都会发出疑问，会不会最终发现不是曹操墓？所以，思想压力也很大。还好，地方政府对我们的支持还比较到位。<br />
<br />
　　价值意义 将解决诸多历史悬案<br />
<br />
　　记：曹操墓的认定有什么价值和意义？<br />
<br />
　　潘：为我们研究帝王陵墓提供了直接证据，特别是这一时期的墓葬没有发现过，更没有发现过帝王陵墓，它有标尺作用。<br />
<br />
　　这一重大发现，印证了文献中对曹操高陵的位置、曹操的谥号、他所倡导的薄葬制度等有关记载是确凿可靠的信史。千百年来，这些问题扑朔迷离，异说种种，疑雾重重，并由此产生了对曹操的许多怀疑和曲解。曹操墓的确认，扫去阴霾，将解决诸多历史悬案。<br />
<br />
　　曹操墓的发现，让我们新获许多历史信息，必将为曹操及汉魏历史的研究开启新的篇章。发掘成果为汉魏考古树立了具有准确年代的标尺，相关领域的研究必将获得新的突破。<br />
<br />
　　记：下一步的打算是什么？<br />
<br />
　　潘：曹操墓的发掘尚有许多工作仍在进行，如陵园建筑、墓地布局的调查等。同时，保护展示规划也已开始着手进行。我后天就要回郑州，自己首先想休息一下。<br />
