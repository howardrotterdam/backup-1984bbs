曹操高陵前室<br />
<br />
<br />
　　中广网北京12月27日消息(记者张庶卓) 据中国之声《央广新闻》10时52分报道，河南省文物局刚刚在北京向新闻媒体公布一项重大考古成果。河南省文物局副局长新闻发言人孙英民宣布重大考古发现，曹操高陵在河南得到考古确认。<br />
<br />
视频：河南安阳曹魏大墓经鉴定墓主为曹操<br />
 <br />
来源：CCTV新闻频道<br />
 　　河南省文物考古研究所在安阳县安丰乡西高穴村抢救性发掘一座东汉大墓获得重大考古发现，经权威考古学家和历史学家根据古资料现场考证研究，认定这座东汉大墓为文献记载中的曹操高陵。<br />
<br />
　　这座大墓在河南省安阳县安丰乡西高穴村南，曾多次被盗，为了及时有效的予以保护，2008年12月经报国家文物局批准，河南省文物局组织河南省文物考古研究所开始对这座墓葬进行抢救性的考古发掘。<br />
<br />
　　这个墓平面为甲字形，坐西向东是一座带斜坡墓道的双室砖墓，规模宏大，结构复杂，主要由墓道前后室和四个侧室构成。斜坡墓道长39.5米，宽9.8米，最深处距离地表大概是15米，墓平面略呈梯形，东边宽22米，西边宽19.5米，东西长18米，大墓占地面积740多平方米。<br />
<br />
　　据了解，墓虽然多次被盗掘，但仍幸存一些重要的随葬品，出土了器物200多件，包括金、银、铜、铁、玉、石、古、漆、陶、云母等多种质地。器内主要有铜带钩、铁甲、铁剑、玉珠、水晶珠、玛瑙珠、石龟、石壁、石枕、刻名石牌等等。<br />
<br />
　　最重要的随葬物品极为珍贵一共有8件，分别刻有“魏武王常用格虎大戟”、“魏武王常用格虎大戟”等铭文。在追缴该墓被盗出土的一件石枕上刻有“魏武王常用慰项石”铭文，这些出土的文字材料为研究确定墓主身份提供了重要的、最直接的历史依据。<br />
<br />
　　在墓室清理当中发现有人头骨、肢骨等部分遗骨，专家初步鉴定为一男两女三个个体，其中墓主人为男性，专家认定年龄在60岁左右，与曹操终年66岁吻合，应该是曹操的遗骨。<br />
<br />
　　记者采访文物界的专家，汉、魏考古专家学者等，他们认为曹操高陵的发现具有极其重要的意义。首先这一重大发现印证了文献中对曹操高陵的位置、曹操的嗜好，他所倡导的薄葬制度等有关记载是确凿可信的。<br />
<br />
　　千百年来，这些问题扑朔迷离、异说种种，由此产生对曹操的许多怀疑和曲解，曹操高陵的认定解决了很多历史悬案。其次曹操高陵的发现获得更多的历史信息，为曹操及汉魏历史的研究开启了一个新篇章。<br />
<br />
　　高陵发掘成果为汉魏考古树立了标准的年代标尺，相关领域的研究必将获得新的突破。曹操是中国历史上著名的政治家，军事家、文学家，其墓葬及有关遗存的保护展示必将受到社会的广泛关注。</div>

							
							
							
							
