													<h2>都被忽悠了?河南安阳的曹操墓疑点重重</h2>
						
						
																											<div id="postmessage_403130" class="t_msgfont">曹操墓被找到的消息一经发布，引起很大的轰动。但笔者以为，文物单位也好，广大网民也好，对此事仍需冷静分析。因为这件事实在是有太多的疑点。<br />
<br />
　　报道中介绍的情况一点颇多。<br />
<br />
　　例如：“据了解，墓虽然多次被盗掘，但仍幸存一些重要的随葬品，出土了器物200多件，包括金、银、铜、铁、玉、石、古、漆、陶、云母等多种质地。器内主要有铜带钩、铁甲、铁剑、玉珠、水晶珠、玛瑙珠、石龟、石壁、石枕、刻名石牌等等。”<br />
<br />
　　既然多次被盗，尚存例如金、银之类贵重物品，岂不让人起疑？<br />
<br />
　　专家也曾提出异议，如果没有确切地证据，还是不要炒作的好。<br />
<br />
　　分享一个网友的帖子：<br />
<br />
　　河南安阳传来消息，称曹操墓已经确认。的确，从出土的石刻上可以看出，无论是字体还是内容，都应该是与曹操有关。但是，个人认为，由此就判断是曹操真墓，尤其是遗骨就是曹操的，还为时过早。<br />
<br />
　　说实话，我对曹操墓还是很有期待的，所期待的是曹操的藏书。曹操不但本人是一个大军事家、大文学家，而且酷爱读书，在其统一北方后，更是想方设法搜集书籍，甚至让蔡琰默写其父的藏书。因为汉魏陵墓多已经被盗，所以说，在江陵焚书之前的古人墓中，除了秦始皇就是曹操的最值得期待。所以在报道中，我没有看到有书的迹象，第一感觉就是很失望。<br />
<br />
　　但是，在失望之后，不禁有些疑惑和新的希望，或者这个仅仅是曹操的疑冢？主要有以下几个疑点：<br />
<br />
　　1、曹操是名人，如果说果真是他的墓，那么多明显的标志，盗墓贼自然会把他的东西卖出个好价钱，得到的人，也一定会把他作为宝贝，千把年了，总归古董界里应该不少，怎么确不见提起？须知，曹魏的西晋存在极短，以后乱世里的盗墓贼，大可以把盗墓的责任推到以前胡人政权的身上。最不济，也应该有个墓志铭留下来啊？<br />
<br />
　　2、曹操是盗墓的行家，可谓老鸟，相信对于各种墓的防盗设施是很通晓的，他的墓应该是机关重重才对，可是这座大墓气势还可以，但是怎么没有什么流沙墓、陷坑之类的机关？从画面上看，塞门的石头也太马虎了。<br />
<br />
　　3、所谓的专家说证据是后世的一块墓志铭，说距离魏武帝多少多少步。有点搞笑啊！曹植都不知道，问他哥哥曹丕，曹丕也没有说。后世怎么知道的？而且清楚得知道是多少步？南北朝时期有历史癖和考据癖的裴松之不知道、范晔不知道，驸马爷是什么东西，倒知道了？<br />
<br />
　　4、回到文字上，忽然发现有破绽，魏武王是没有错的，封国在魏，谥号为武，但是，为什么在他自己的墓地里出现？生怕他老人家认不出自己的兵器，还是给别人看的？总不会是给别的鬼看的吧？请问，历代墓地里有给墓主自己的东西贴上谥号标签的？是不是让后世的人看啊？办展览啊？<br />
<br />
　　5、一个男人，两个女人的头盖骨，看起来振振有词，可是粗知历史的人都知道。曹操的原配丁夫人早已经被谴回娘家，后来的卞皇后一直活到孙子曹敝凑的太和四年才辞世，除了她老人家，谁有资格和曹操同穴？要是卞皇后有那么大度，史家为什么不记载？别告诉我另一个是丁夫人，就算丁夫人同葬，也应该恢复名分的。<br />
<br />
　　6、是哪个有文化有水平的盗墓贼，把书都搬走了？又就此没有了？！<br />
<br />
　　综合以上所述。笔者有理由认为，安阳现有的墓不是曹操真墓，倒可能是两种情况。<br />
<br />
　　第一种，比较悲观的。是一个南北朝时期的墓，这个人对曹操比较感兴趣，所以收集了曹操的兵器，办了个陈列室，所以有那些给人看的石器。或者说，干脆，就是好事之徒伪造的，一不小心露了马脚。，曹操的墓，还得慢慢找。<br />
<br />
　　第二种，比较乐观的。是曹操自己设立的疑冢，让别人盗了这个假的，断了心思，所以就可以解释，怎么有个六十多岁的家伙，也可以解释为什么自己给自己的兵器贴标签，还可以解释为什么什么机关都不设以及为什么没有书籍陪葬（那个时候书可宝贵着呢！）为什么说这个推论比较乐观呢？那就是距离真的曹操墓不远了，因为这么精心的安排只有放在真墓附近才能发挥更大效能。这样的假墓，就是无论是盗墓的，还是奉旨鞭尸的，即便知道真墓的大体位置也会挖错。个人认为，很可能就在西侧，下方若干米。不过开挖的时候要注意啊，老曹是很厉害的，那里面肯定是集三国以前防盗之大成啊。<br />
<br />
新发现的曹操墓墓室<br />
<br />
新发现的曹操墓深深的墓道<br />
<br />
　　这次考古的遗址上，发现了几座古墓，其中，二号墓被定为曹操墓。而考古专家认定的依据，来源于出土的一块石碑，上面有这样的字样：“魏武王常所用虎大戟”。与史料对比，这就是曹操生前的封号。据二十四史之一的《三国志》，开篇第一卷就是“武帝纪”。<br />
<br />
　　虽然曹操被史家称为魏武帝，曹操实际上一天皇帝也没有当过，这是他大儿子曹丕当了皇帝后，追谥的。曹操真正的最高封号是“魏王”。曹操生前确实也用过“虎大戟”等兵器，由此可以证明，这是曹操墓中的随葬物。<br />
<br />
曹操墓考古现场<br />
<br />
　　据现存典章分析，在隋唐时，曹操的陵墓确实并非“疑冢”。《全唐文》卷十之《唐太宗文皇帝祭魏太祖武皇帝文》，证明了这一点。贞观十九年（公元645 年），唐太宗李世民亲征高丽，路过邺地时曾拜谒高陵，并亲撰祭曹操，称曹操“昔汉室三分，群雄并立。夫民离政乱，安之者哲人；德丧时危，定之者贤辅。伊尹之匡殷室，王道昏而复明；霍光之佐汉朝，皇纲否而还泰。”评价甚高。<br />
<br />
　　其实，早在十多年前，河南的考古专家便盯上了目前认定的曹操墓。 1998年，曾出土了一块后赵建武十一年（公元345年）鲁潜墓志，上有“故魏武帝陵西北角西行四十三步，北回至墓明堂二百五十步”字样，与高陵在“邺城西”的记载基本上是一致的。从中亦可见，当年曹操并没有秘葬，大家都知道高陵在哪。<br />
