																											<div id="postmessage_93169" class="t_msgfont">BEIJING, May 14 (Reuters) - Two decades after his downfall and four years after his death, reformist Chinese leader Zhao Ziyang has broken the official silence on the 1989 Tiananmen crackdown, denouncing the killings of protesters as a &quot;tragedy&quot;.<br />
In memoirs recorded secretly under house arrest, Zhao has challenged China's cautious, current leaders just before the 20th anniversary of June 4, when troops crushed pro-democracy protests centred on Tiananmen Square in Beijing.<br />
He praises Western-style democracy and denounces the armed quelling of the protests, when troops and tanks pushed down Chang'an Avenue, shooting demonstrators and onlookers.<br />
&quot;On the night of June 3rd, while sitting in the courtyard with my family, I heard intense gunfire,&quot; says Zhao. &quot;A tragedy to shock the world had not been averted.&quot;<br />
Zhao, who was head of the Communist Party in 1989, rejects the government's claim that the student protesters were part of an anti-Communist conspiracy.<br />
&quot;I had said at the time that most people were only asking us to correct our flaws, not attempting to overthrow our political system,&quot; Zhao says in the book &quot;Prisoner of the State&quot;, to be published by Simon &amp; Schuster in English this month ahead of the 20th anniversary.<br />
The memoirs, about 30 hours of tape, were given to three confidants and smuggled out of China. A manuscript was obtained by Reuters. [ID:nSP135799]<br />
Zhao's account of Party elders pushing him from power sheds rare light on the political warring behind the protests that shook China 20 years ago, culminating in his ouster and the crackdown that killed hundreds on the streets of Beijing.<br />
&quot;I told myself that no matter what, I refused to become the (Party) general secretary who mobilised the military to crack down on students,&quot; he says.<br />
Zhao had his eyes fixed on China's future when he secretively recorded his memories throughout years under house detention until his death in January 2005. He decries what he saw as the mistaken conservative path taken by the Party after 1989 and argues for a gradual transition to Western-style democracy.<br />
&quot;In fact, it is the Western parliamentary democratic system that has demonstrated the most vitality,&quot; says Zhao.<br />
<br />
&quot;If we don't move toward this goal, it will be impossible to resolve the abnormal conditions in China's market economy.&quot;<br />
<br />
China's current leaders brush aside the &quot;disturbance&quot; of 20 years ago as a distant event with a settled official verdict, and Zhao's book is sure to be banned by authorities who will seek to stop copies of the Chinese edition slipping into the mainland.<br />
<br />
But Zhao remains a symbol of reformist rectitude to sympathisers and, with even apolitical citizens eager to learn about the Party's secretive ways, copies may still spread.<br />
<br />
Bao Pu, a Hong Kong-based publisher and son of Zhao's former top aide, said Zhao apparently wanted to give his version of events to challenge the Party's official condemnation of the Tiananmen protesters and its one-Party rule.&nbsp;&nbsp;Continued...<br />
<br />
&quot;He did not leave instructions ... but clearly he wanted his story to survive,&quot; said Bao, whose New Century Press is publishing the Chinese edition of the book.<br />
<br />
&quot;It's a crucial period of history that defines modern day China. It contradicts the government's version of the truth.&quot;<br />
<br />
Bao Pu's father, Bao Tong, lives under police surveillance in Beijing but has been allowed to meet foreign reporters. [ID:nSP91291]<br />
<br />
BREAKING WITH DENG<br />
<br />
The thread running through Zhao's memories of his rise and fall is his tortured bond with Deng Xiaoping, the wizened revolutionary veteran who steered China to market reforms but rejected -- ultimately with force -- calls for democratic change.<br />
<br />
Deng is honoured by China as the pioneer behind the country's economic success, and Zhao's account of double-crossing and betrayal under Deng is likely to irk the country's current leaders, who like to present an image of solid unity.<br />
<br />
Zhao rejects the notion Deng was instinctively in favour of political relaxation but was led astray by conservatives.<br />
<br />
&quot;Deng had always stood out among the Party elders as the one who emphasised the means of dictatorship. He often reminded people about its usefulness,&quot; says Zhao.<br />
<br />
Deng's notions of democracy &quot;were no more than empty words&quot;.<br />
<br />
Deng was paramount among Party elders who dominated behind the scenes while Zhao and his colleague, Hu Yaobang, coaxed officials to break up rural communes and strictures on private business that Communist leader Mao Zedong made his legacy.<br />
<br />
But by the late 1980s, Zhao found it increasingly difficult to weave between conservatives enraged by the crumbling of Soviet socialism and the advances of market reforms and intellectuals and advisers who wanted to push past barriers to economic and then political liberalisation.<br />
<br />
Zhao says that in ousting him from power, Deng, then-premier Li Peng and Party conservatives trampled on rules meant to prevent a return to Mao's years of arbitrary, one-man power.<br />
<br />
The remedy to China's problems, Zhao says, lies in gradual but unceasing movement towards democracy.<br />
<br />
&quot;I believe the time has come for us to tackle this issue seriously,&quot; he concludes. (Additional reporting by Ben Blanchard in Beijing and James Pomfret in Hong Kong; Editing by Nick Macfie and Dean Yates)</div>

							
							
							
							
