																											<div id="postmessage_816832" class="t_msgfont">海尔冰箱半年内两少女触电身亡 质量问题尚需重锤<br />
来源：新华网<br />
2010年09月05日09:16<br />
<br />
　　国内冰箱行业的龙头企业海尔再度出现严重的质量问题，8月16日，江西省永丰县一名11岁女孩在靠近海尔冰箱捡东西时不幸触电身亡，这已经是今年内第二起少女因海尔冰箱触电身亡事件。当年张瑞敏曾抡起铁锤砸掉质量不合格的海尔冰箱，成为一段广泛流传的企业传奇，如今海尔冰箱已经成为行业销量第一，却依旧发生这么严重的事故，难道还需要张瑞敏再挥动铁锤才能砸去海尔冰箱的质量问题？<br />
<br />
　　半年时间两少女触电身亡<br />
<br />
　　据山东新闻网报道，2010年8月16日，江西省永丰县一女孩在靠近海尔电冰箱捡东西时，被带有强大电流的冰箱外壳牢牢吸住，11岁少女不幸触电身亡。据悉，死者刘思清家的海尔冰箱型号为BCD-196TDXZ，已用了一年多的时间。期间，曾有村民多次向刘思清父母反映，当靠近冰箱时会有触电的感觉，但碍于两位家长多次亲身靠近冰箱体验时，未曾出现触电感觉，于是该隐患一直潜伏到11岁幼女丧命之时。<br />
<br />
　　此前据广东梅州电视台报道，2010年2月1日，梅县石扇村南村一名年仅8岁的少女叶燕晖在开海尔冰箱时触电身亡。叶燕晖的父亲告诉记者，这台冰箱是他于去年8月29日在梅城一商行购买的。买回来没几天，就发现冰箱有漏电问题。海尔经销商却答复：“这是超低压的，没什么关系。”叶燕晖的家属说，在得到经销商的答复后，他们也就放下了心，对冰箱的放电现象不以为意，每天都照常使用。但让他们做梦也想不到的是，就是这台冰箱，带走了他们唯一的孩子。<br />
<br />
　　短短半年多的时间内，就有两位花季少女因为海尔冰箱的漏电问题而触电身亡，失去了鲜活无辜的生命，也给家人带去了无尽的悲痛。而背后体现出来的，是海尔缺乏家电大企业的自觉，漠视质量问题，导致事故频发。<br />
<br />
　　转型转出的质量问题<br />
<br />
　　据青岛海尔近期发布的上市公司中报显示，2010年上半年海尔冰箱营业收入达到131.32亿元，同比增长37.85%。中怡康发布的统计数据显示，上半年海尔冰箱市场零售额份额为25.21%，市场龙头地位稳固。然而，这些漂亮的数字，并不能掩盖海尔冰箱的质量问，甚至为更多消费者带来隐忧。<br />
<br />
　　作为“中国制造”的代表型企业之一，近年来海尔开始提出从单纯的制造业向服务业转型，从卖产品向卖服务转型。在海尔的“转型”战略中，非常重要的一点在于，海尔将逐渐淡出生产制造业务，将其生产环节外包，以实现从制造型企业向营销型企业的转型。据业内人士分析，一方面，在服务转型途中，海尔正经历着“即需即供”、“零库存”的转型之痛；另一方面，海尔也因为质量的不可控与服务的不规范，正在经历消费者投诉的“质量之痛”。<br />
<br />
　　而海尔冰箱的质量问题，也使得消费者开始效仿张瑞敏，用铁锤给海尔公司敲响质量警钟：据湖北媒体报道，2009年3月9日，在湖北武汉，武昌徐东路一家电器商场内，顾客卢女士抡起一把铁锤，将卖场的3台海尔电冰箱当众砸坏。海尔冰箱之所以被砸，是因为卢女士家中海尔冰箱突然失火引发火灾，导致财务受损索赔未果所致。<br />
<br />
　　海尔的真诚到底有多远<br />
<br />
　　海尔在转型之前，就大力宣扬其优质服务，“海尔，真诚到永远”更广为流传的知名广告语。然而，在屡屡发生的质量问题，甚至导致消费者死亡的极端质量事故后，海尔的服务态度却是“有多远，离多远”。<br />
<br />
　　江西永丰少女刘思清触电身亡事件中，在鉴定结果出来后，刘思清家人和永丰县海尔专卖店负责人协商赔偿一事。对方要求他们上交一份详细的事故报告说明事情的来龙去脉，并提出自己的诉求。然而刘思清姨丈上交的事故报告当天被驳回，海尔一方要求刘思清家人将报告标题中提到的“补偿”两字改为“援助”，否则，协商到此为止。从“补偿”到“援助”，是从服务者的角度到高高在上的施舍者角度，更是想将自己的责任推卸得一干二净。<br />
<br />
　　而在广东梅州少女叶燕晖触电身亡事件中，死者的家属认为酿成悲剧的原因跟冰箱的质量有关，当着记者的面，死者的家属也再次拨通了经销商的电话，在电话中，经销商承认，当初确实接到过死者家属关于冰箱漏电投诉电话。但经销商认为，当初反映的情况并不属于漏电，而是正常的静电现象。“正常的静电现象”却导致触电身亡事故，正是这种漠视的态度，导致本来完全有可能避免的惨剧最终发生。 (来源：中国经济网) <br />
