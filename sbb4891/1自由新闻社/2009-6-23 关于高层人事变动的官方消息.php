																											<div id="postmessage_133271" class="t_msgfont">北京心莲百合文化交流中心（以下简称“中心”）成立于2006年4月30日，是在国家文化体制改革的背景下产生的，致力于民族文化的研究和弘扬，是从事公益活动的非盈利性义务服务单位。<br />
