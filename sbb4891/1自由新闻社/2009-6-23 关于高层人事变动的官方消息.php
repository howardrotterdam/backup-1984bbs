答：大家不太了解政治，不太了解行政，我没有任何风险。因为我的职责、我的义务就要求我必须那样讲。如果我不那样讲的话，恰恰是我不负责任的表现。如果我不负责任，就说明我不称职，那么，那就是我真正的风险。大家知道了吗？大家太不了解我们的社会了。<br />
<br />
 <br />
<br />
问：郭主任,您既是党员又是居士,不矛盾吧?<br />
<br />
 <br />
<br />
答：如果大家知道我是党员，那我就不是居士了。如果大家不知道我是党员，那我就是居士。这是什么原因呢？大家别忘了，我在搞国家哲学科研，科研是保密的。既然保密，就会有很多很多的乔装做掩护。<br />
<br />
 <br />
<br />
问：主任您好：我们总想听到您的心路历程，可是不知道用什么语言才能真心打动您呢？<br />
<br />
 <br />
<br />
答：你们说的话感动我，是这个意思吗？你们好好地提高自己的修养，就能感动我。你用花言巧语感动不了我。大家可别忘了，我研究心理这么多年，我搞侦察工作这么多年，你说什么样的话，我一目了然，一下子就能洞穿你的内心世界。所以，不用你说话，通过你的举止我对你已经了解了很多。大家别忘了，这是我的专业。你想打动我，唯一的就是你的人品能打动我。唯有你真正地付出才能打动我，唯有你的高尚品德才能打动我，否则无效。你用什么样的花言巧语，你自以为多么高明，对我都无效。<br />
<br />
 <br />
<br />
问：在房间里，我们个人所说、所反应的现状，是否受到保护？是否触犯了法律法规？<br />
<br />
 <br />
<br />
答：这个问题是这样的。我们生活在社会里面，这个社会它是一个机制，你和法律相较而言，法律既保护你，又监督你。所以，在房间里的每一个人所反应的现状，都受法律保护，同时，如果你反应的现状不是真正的现状，或者说涉及到违法犯罪问题了，那么你就触犯了法律，那么你必然会受到法律的惩罚。我们中心有了《免责声明》――不对你的个人言论负责。因为我们中心必须要甩开包袱，轻装上阵，才能真正地把事业搞好。不能带着包袱工作！所以要求我们网友要克服习气，去掉那些偏激的个性，要真正地把修养提高上来，要懂政治。这样才能行。<br />
<br />
 <br />
<br />
问：主任您好！我们有的战友在挫折中有放弃学习的念头了；在您过去艰苦的九年里，支持您“不放弃”的就一个原因吗？<br />
<br />
 <br />
<br />
答：其实，支持我“不放弃”的当然不仅仅是一个原因了。我提到一个主要原因就是大家的言行和对我的信任感动了我。另外一个呢？就是高姿态。“军人修养”里面有一种修养就是“高姿态”，就是“胸襟”，就是谦让老百姓。那么，比如说提到一个问题，国家对我不好，他们做得很差劲，有些人对我很不好，我怎么想呢？――高姿态，不和他们一般见识！这就是我们军人所应该具有的品德修养。只有这样才能够不被任何困难所击倒，永不放弃。我再重复一下：要有胸襟，不要和他们一般见识！<br />
<br />
 <br />
<br />
问：请问：在中心的工作加重时，生活中的问题、需要做的工作也增多，感觉手忙脚乱，这个时候怎样突破呢？<br />
<br />
 <br />
<br />
答：用“军人修养”来指导你的工作修养。比如说，你把“军人修养”（敬业精神、雷厉风行、注重细节、主次分明、管理时间等等）放到你的工作修养中。另外，你的生活修养也要通过“军人修养”来指导。学会突破，不要把“军人修养”和工作、学习、生活脱节。你的工作修养、生活修养都要用“军人修养”来指导。这个时候很多问题就会迎刃而解。<br />
<br />
 <br />
<br />
问：主任您好！在听课中，感觉我们的国家真的面临很大的道德危机，坚持道德原则的人们，就是遏制道德下滑的中流砥柱，请您讲一讲信念与希望可以吗？<br />
<br />
 <br />
<br />
答：可以。我用八个字来总结一下：“星星之火，可以燎原”。有这一点，大家就可以坚定信念和希望了。<br />
<br />
 <br />
<br />
问：郭主任你有小孩吗？如有小孩多大了？<br />
<br />
答：我没有小孩，还没要孩子呢。<br />
<br />
 <br />
<br />
问：主任，现在有个人想要见到您，是否有一定的成绩才可以有资格申请呀？<br />
<br />
 <br />
<br />
答：对。因为我们要照顾到社会的情绪。也要照顾到社会的影响。所以，我现在不轻易见任何人了（我只能是接见别人）。所以，你要想见我的话，只能是你有很好的成绩才可以。大家要注意，这是因为社会情况决定的。特别是我要照顾到社会情绪。你要来找我的话，可能会引起很多人不满――凭什么要见你呢？到底你有什么能力呢？所以，要照顾到社会情绪。因此，你只能好好地工作、好好地学习、好好地提高修养。有了成绩之后，你才能够申请。<br />
<br />
