&nbsp; &nbsp; 心莲百合文化网是北京心莲百合文化交流中心的官方网站，是作为公益准军事化交流的门户。以弘扬民族文化、携手民族复兴为宗旨。结合时代人文特点，我们对传统优秀的国学文化进行了阐释与挖掘，以全新的管理模式、丰富而健康向上的文化内容、方便快捷的学习交流形式呈献给社会各界朋友。在这里驻足，您可以得到心灵的陶冶。这里帮助您逐步学会如何驾驭自己的内心世界，如何以洒脱的心态生活和融于社会。</div>

							
							
							
							
