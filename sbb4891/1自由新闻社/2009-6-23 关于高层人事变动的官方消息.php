													<h2>蓝莲花简介</h2>
						
						
																											<div id="postmessage_133273" class="t_msgfont"><strong>&nbsp; &nbsp; 蓝莲花，实名郭旭东，男，现役军官，中央军委参谋，总部级，上将军衔，年轻的共和国将军；特警指挥官，总警监警衔；中共党员，国家某部政委；哲学博士，国家哲学顾问，国家哲学与先进文化科研顾问。<br />
</strong><br />
1971年11月出生。成长于革命老干部家庭。其父毕业于时任校长为陈毅的华东军政大学，为曾参加过淮海战役和抗美援朝战役的战斗英雄。因11岁时父亲病故，由国家抚养至大学毕业。受家庭的熏陶，自小崇尚中华民族的优良传统道德。<br />
&nbsp; &nbsp; 1995年6月毕业于河北医科大学中医学院。1995年7月参军，任某特种部队侦察员。1998年同时从事军事哲学研究工作。2000年2月调入中央军委某集团军，出任指挥官，并继续研究军事哲学，同时出任国家哲学（含军事哲学）顾问至今。2002年出任国家哲学（含军事哲学）与先进文化（含军事文化）科研顾问至今。现兼任国家某部政委，太原中旅集团、银建房屋开发有限公司、中银房地产开发有限公司、锦麟东方酒店有限公司独立董事（义务），太原银建房屋开发有限公司特聘名誉国学顾问，石家庄广亚贸易有限公司企业文化名誉顾问，上海禅悦贸易有限公司名誉保健顾问，北京心莲百合文化交流中心主任。<br />
<br />
&nbsp; &nbsp; 三年来，郭主任接受大众的热情邀请，坚持义务服务社会，通过网络义务为大众进行医学、文学、心理学、哲学、伦理学、政治、英语、军事文化等课程的讲座1000余学时。同时，也经常义务为企业宣讲企业文化和军事文化。郭主任已经出版的著作有《郭旭东中医文化随笔》、《郭旭东诗音》，网络出版的著作有《新时代的诗音2》、《民间故事选集》、《郭旭东博文选集1》、《郭旭东博文选集2》，网络发表的著作有《道德面面观讲评》、《心灵救护》、《郭旭东语音解答的问题》等，无偿捐献给国家的百万字著作有《哲学与传统先进文化讲义系列》。<br />
<br />
&nbsp; &nbsp; 近年来，郭主任进一步侧重军事哲学、军地两用人才建设、兵商文化以及其他军事文化的研究，网络发表了《军人修养》、《军人的话》、《军阀习气》、《带兵小议》、《军民情深》、《列兵之魂》、《另类武器》等著作，正在发表的有《孙子今读》等著作。 <br />
<br />
&nbsp; &nbsp; 近一年来，郭主任向党中央提供了具有参考价值的解决国计民生问题的大量建议和方案，仅2009年前几个月就提供了一百五十余项。</div>

							
							
							
							
