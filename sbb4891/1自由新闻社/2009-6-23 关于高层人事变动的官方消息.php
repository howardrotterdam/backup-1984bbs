																											<div id="postmessage_133330" class="t_msgfont">蓝莲花专访（关于吴仪邪恶团伙问题的答疑）<br />
<br />
<br />
2009年6月21日晚，北京心莲百合文化交流中心主任郭旭东将军就吴仪邪恶团伙问题回答了现场网友的提问。以下的“问”指现场网友的提问，“答”指郭旭东将军的现场解答。<br />
<br />
<br />
问：请问：“吴仪篡党夺权”这样的事件往往都是国家发布。为什么在我们网站首先发布呢？<br />
<br />
答：我说过很多次了，“家丑不可外扬”。吴仪篡党夺权这件事情在我们国家是非常丢人的事情，无论是中央、无论是国家都感觉非常地丢人，出现这样的事情不愿意向外公布，大家要注意。至于将来公布不公布那得看实际情况了。再说之所以由我公布，是因为抓捕吴仪邪恶团伙是由我指挥的。况且我向中央军委的二九年第八百四十几号请示被批复：公布！公布多少内容那都一清二楚的。另外我说过了，我们的网站是权威，很多信息都是先从我们网站公布出去的，很多普通网站是不知道这些内容的，大家要注意。所以我们要增加政治敏感度，真正有政治嗅觉的那些领导干部都是看我们的网站的。大家要注意这一点。<br />
<br />
问：请问郭将军，前些日子对您绑架、暗杀、投毒的，是吴仪邪恶团伙干的吗？她（他）们邪恶到了巅峰也是指这些行为吗？<br />
<br />
答：关于前些日子对我绑架、暗杀、投毒，确实是吴仪邪恶团伙干的。但是，这不是她（他）们的巅峰行为，这是她（他）们的普通行为。十年来，她（他）们都是这样做的。她们的行为我们可以用四个字来概括，叫“令人发指”！还可以用四个字来概括，叫“罪恶滔天”！这些行为对她（他）们来讲是很普通的。吴仪就是这样的。所谓“巅峰状态”比这还要厉害。大家要注意这一点。这十年来，她（他）们对我都是这样的，并不是说最近才这样，才对我进行绑架、暗杀、投毒的。十年来她（他）们都是这么做的，我随时都是在被绑架、暗杀、投毒这样的生命危险中。吴仪就是这样的人，并不只针对我，对很多很多人都是这样的。所以，有朋友问我说，吴仪是不是比妲妃还坏？和妲妃相比，她比妲妃坏许多倍。大家要注意这一点。因为妲妃没有权，而她有实权，她自己亲自指挥来做这些坏事，而且道貌岸然。表面上她把自己打扮成一个好人，背地里做了无数坏事，而且都是令人发指的。什么绑架啊、杀人啊、陷害人啊，在她（他）们眼里是司空见惯的事情了，根本不足为怪。<br />
<br />
问：请问：“吴仪篡党夺权”的事情算我党内的政治斗争吗？<br />
<br />
答：不属于政治斗争。是因为她（他）们的无数恶行触犯了刑法，受到了法律的严惩。<br />
<br />
问：郭将军，您说自己被绑架、暗杀、投毒，可是我们好象并没有看到您耽误给大家上课？<br />
<br />
答：这是因为我身边一直有中央军委在保护我，有我的战友在保护我，所以，她（他）们的这些罪恶行径才没有得逞。<br />
<br />
问：国家为了表示对反腐的打击力度和决心，应大力宣传吴仪问题，不存在家丑不可外扬的问题，原来国家就公布了很多高官腐败问题。请您解释一下？<br />
<br />
答：高官腐败问题毕竟是腐败，那么吴仪邪恶团伙呢，是因为她（他）们的很多罪行罪恶滔天。对于罪恶滔天的行为和这样恶毒的团伙，我们用了十年的时间才发现，难道不是“家丑”？这样的“家丑”外扬当然不好了，是很难令人启齿的。大家要知道，用十年的时间，才说是“家丑不可外扬”嘛。她（他）们涉及“家丑不可外扬”的问题，用了十年的时间才发现，才被打掉，才被铲除！<br />
<br />
问：吴仪她（他）们害人是针对您的呢，还是针对很多人？她（他）们怎么隐蔽这么深呢？<br />
<br />
答：当然针对我，同时也针对更多善良的人。很多人都是被吴仪邪恶团伙陷害的，这些人现在还在以泪洗面。很多人都被陷害得非常地惨。那么，为什么她（他）们隐蔽得这么深呢？很简单，阴谋嘛，阴谋就是隐蔽得很深的！<br />
<br />
问：吴仪她（他）们的阴谋是您发现的吗？<br />
<br />
答：吴仪她（他）们的阴谋，确实是我发现的。之后，我报告给了中央军委。中央军委做出批示：由我来指挥，铲除吴仪她（他）们一伙儿。<br />
<br />
问：吴仪都七十多岁，而且辞去了一切职务，还争什么呢？<br />
<br />
答：七十多岁在吴仪她（他）们的眼里，作为国家领导人不算什么多大的年龄，大家要注意。另外，所谓吴仪辞去了一切职务，这些新闻都是假的。她根本没有辞去职务，被抓前还在正常地工作，还在主抓很多工作。<br />
<br />
问：您说吴仪一伙掌握了我们国家的命脉，是经济命脉吗？<br />
<br />
答：既然是国家的命脉，就不是经济命脉，是国家重要的机要部门。她（他）们一伙控制了国家重要的机要部门，那些才是国家的命脉。<br />
<br />
问：郭将军，您那天说新闻媒体是被吴仪她（他）们控制的，请问如何对新闻媒体进行拨乱反正呢？如何表达事实呢？<br />
<br />
答：这个问题是这样的：实际上以后新闻媒体进行拨乱反正，首先得自查自纠，自己就知道如何拨乱反正。这个问题就说到这吧。<br />
<br />
问：真想不通，吴仪为什么要篡党夺权？一个女人做一个副总理不挺好吗？已经到权力的顶点了，难道想当女皇帝？这个事情不那么简单吧？<br />
<br />
答：因为民间有传说。民间什么传说呢？民间说，“未来的皇帝是个女人，是个女皇帝”。吴仪利用这些“传说”，一直为自己做“皇上”做准备。所以，她（他）们一伙用了十年的时间来谋划，来网罗人马，来篡党夺权。大家清楚了吗？而且她（他）们还到处散播“未来的皇帝是个女人”这样的言论。<br />
<br />
问：请问郭将军，吴仪是“走火入魔”了吗？<br />
<br />
答：不是“走火入魔”，吴仪是想借助民间传说来提高自己的威信，从而为自己篡党夺权做准备。<br />
<br />
问：为什么在百姓眼里，吴仪她（他）们的阴谋一点儿也没有迹象啊？<br />
<br />
答：首先因为吴仪离你很遥远，你根本就不知道她的真实情况。另外呢，吴仪控制着媒体，大家要注意，她想让媒体怎么说，媒体就怎么说。那媒体当然不会报道她任何负的方面了。再有一点，吴仪她善于伪装，你当然一点儿迹象也不能发现了。<br />
