													<h2>周日是关键的一天</h2>
						
						
																											<div id="postmessage_399089" class="t_msgfont">伊朗安全部队与示威者在德黑兰发生冲突<br />
<br />
记者: 叶拉尼安 | 开罗 2009年 12月 26日 <br />
<br />
<br />
目击者报告说，在德黑兰部分地区和一些省的城市，人群与伊朗安全部队星期六发生冲突。据报导，警方向示威者挥舞警棍，砸碎汽车玻璃，并使用催泪瓦斯和辣椒喷雾来驱散人群。<br />
<br />
星期六在伊朗首都德黑兰，几千名示威者和安全部队发生激烈冲突。冲突后来持续升级并绵延到了更多地点。<br />
<br />
几家反对派网站报告说，忠于政府的革命卫队和志愿的巴斯基民兵全部出动，在首都的几个地点和示威者对抗，他们使用催泪瓦斯、水枪和辣椒喷雾来驱散人群。<br />
<br />
其他的报导说，挥舞着警棍的便衣巴斯基民兵砸碎汽车车窗，并攻击那些按喇叭表示支持示威者的驱车者。<br />
<br />
*示威者高呼反政府口号*<br />
<br />
一名女目击者告诉法达电台说，革命卫队成员粗暴对待高呼反政府口号的示威者。<br />
<br />
她说，冲突特别发生在波尔乔比和瓦利阿斯尔广场周围。她说他看见示威者为今天的斋戒日身穿黑色衣服，这些示威者被革命卫队士兵推搡和抓住。她说，她在大学周围看见另一群抗议者聚集在弗克赫拉齐大道旁，这些示威者高呼“打倒独裁者”的口号。她补充说，政府部队骑着摩托车迅速赶到那些有抗议活动的地点。 <br />
<br />
在发给美国之音的一段录像中，一群年轻人在一辆城市巴士上高呼反对最高领导人哈梅内伊的口号。目击者报告说，抗议者把阿亚图拉.哈梅内伊比作亚齐，他是七世纪的伊斯兰领袖，也是什叶派极其痛恨的人，因为他在杀害侯赛因中起了作用，侯赛因是先知穆罕默德的孙子。<br />
<br />
在发给美国之音的另一段录像中，另一群年轻人聚集在伊朗政府电视台的一个办公室前，同样也可以听到他们高呼“打倒独裁者”的口号。<br />
<br />
*安全部队为之震惊*<br />
<br />
来自英国伯明翰大学的卢卡斯在伊朗人气博客“永久的美国”中写到反对派滚动式的示威活动使得政府安全部队为之震惊。<br />
<br />
卢卡斯说：“直到今天晚上，因特网上没有任何关于集会的消息。这可能是自发的，或者是通过在伊朗的一个网络，而这个网络非常原始，消息根本通不过这些渠道，不像我们平时会知道这些人上哪儿去集合。所以，要是你要查在德黑兰的地点，你得到的是今天的地点，而不是原来游行的地点，所以他们就去玛目侯赛因广场，可是那儿正是发生严重冲突的地方，后来在费尔多西广场和革命广场也都发生了冲突。也就是说，游行在各地轮流进行。”<br />
<br />
卢卡斯一直在密切注视示威的进展。他说，政府在设法组织反集会方面似乎落后于抗议者。<br />
<br />
他说：“就集会来说，政府没有宣布什么消息。就宣传来说，我认为他们落在后头。我认为，他们现在没有派大批人出来宣传。没有。目前所有的地方似乎都向反对派开放。”<br />
<br />
阿拉伯卫视台的报导说，亲政府的革命卫队星期天对预期举行的示威警戒极高。当天，人们要纪念什叶派神圣的阿舒拉节，也是悼念深得人心的蒙塔泽里逝世第七天。分析人士说，星期天对政府来说是一个考验，它将证明政府是否还控制着伊朗的街道。</div>

							
							
							
							
