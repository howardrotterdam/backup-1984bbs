时事风云 | 2009.12.22 <br />
伊朗反对派再度举行抗议活动 <br />
 <br />
Gro&szlig;ansicht des Bildes mit der Bildunterschrift: 伊朗反对派大示威伊朗反对派利用为大阿亚图拉蒙塔泽里举行葬礼的机会进行了抗议活动。在反对派经营的网页上可以看到，周一，有数万人为这位改革派政治家送葬。据称，警方对打出反对艾哈迈迪内贾德总统标语的示威者动用了武力。伊朗政府禁止外国媒体报导该类事件。目前，伊朗反对派仍然活跃，他们要尽最大可能利用政府留下的有限空间。记者担心，在未来数天内，伊朗还将发生大规模的骚乱。 <br />
<br />
如果跟伊朗的反对派交谈，他们的态度依旧坚决。许多对政治感兴趣的人不能忘怀去年六月的那些日子，数以十万计的伊朗民众走上街头，抗议总统选举中出现的作弊现象。然而，半年后的今天，对他们而言，大型抗议活动的踪影已是荡然无存。&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; <br />
<br />
如果谁希望对伊朗的情况进行正确的评价，那么他不该用德国的尺度去衡量。伊朗伊斯兰共和国同德国的民主体制迥然不同，因此，穆萨维和卡鲁比领导的绿色运动不得不时刻防备，不要同强硬的国家政权正面冲突。夏天的抗议活动已经造成二十多人死亡，80余人受到长期监禁，5人被判了死刑。<br />
<br />
鉴于艰险的国内条件，伊朗反对派只有在风险较小的情况下才出现在公众的视野。只有在官方许可的游行集会上，反对派才走上街头，比如纪念伊朗大学生冲击美国大使馆30周年纪念之际或者纪念东耶路撒冷被占领的时候。现在，图拉蒙塔泽里的葬礼也是这样的时刻。为这样一名声望极高的宗教人士举行葬礼，虽然他也是一名政府批评人士，但一般估计，警方不会横加干预。出于这种考虑，数万人加入送葬的队伍，他们借此显示，伊朗的反对派力量依旧壮观。<br />
<br />
图拉蒙塔泽里在生前曾批评国家政权镇压游行群众是在滥用伊斯兰教的名义。今后将会显示，他的离世是绿色运动的一大损失。有两点现在就很清楚，其一是，不论伊朗反对派什么时候在公众社会出现，都会唤起大批民众的支持。其二是，伊朗在未来数天将持续紧张。因为图拉蒙塔泽里去世的日子正好在什叶派穆斯林哀悼死者的圣月里，而这个圣月的高峰是即将到来的星期天。当年伊朗国王统治期间，这一天曾数度发生骚乱。也许现在也到了这样的时刻。至少伊朗国内的气氛让人为此担心。<br />
