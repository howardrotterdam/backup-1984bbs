我想告诉大家，我的姐姐有着远大的梦想…… <br />
我想告诉大家，我去世的姐姐是个好人……她和我一样，期望着有一天，我们能自由地任风吹拂我们的长发；她和我一样，阅读福露格的诗歌；她渴望能过上自由和平等的生活……她渴望能有一天昂起头来，骄傲地说：“我是一个伊朗人”……她渴望能有一天和一个头发蓬乱的男子坠入爱河……她渴望能生一个女儿、为她梳头、在她的摇篮边哼着歌儿哄她入睡…… <br />
