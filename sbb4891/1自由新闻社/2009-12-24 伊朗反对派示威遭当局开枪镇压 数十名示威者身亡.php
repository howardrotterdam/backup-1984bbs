伊朗异议教士去世 众多反对派参加葬礼<br />
记者: 美国之音 | 华盛顿<br />
<br />
<img src="http://media.voanews.com/images/480*320/ap_iran_montazeri_funeral_21dec09_480.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
成千上万伊朗人参加大阿亚图拉.蒙塔泽里葬礼<br />
<br />
伊朗反对派的网站报导说，成千上万伊朗人参加异议教士大阿亚图拉.蒙塔泽里的葬礼。报导说，星期一在伊朗北部圣城库姆举行的葬礼上，许多致哀者高喊反政府口号，手持伊朗反对派运动的绿色标志。<br />
<br />
报导说，防暴警察跟投掷石头的抗议者发生冲突。没有独立消息证实出席葬礼人数或是抗议活动。外国媒体被禁止前往报导葬礼。<br />
<br />
蒙塔泽里被安葬在库姆圣地。他于星期六晚去世，终年87岁。<br />
<br />
伊朗改革派将蒙塔泽里看作他们运动的精神支柱。今年早些时候，蒙塔泽里谴责政府镇压反对伊朗总统内贾德通过有争议大选再次当选的抗议者。<br />
