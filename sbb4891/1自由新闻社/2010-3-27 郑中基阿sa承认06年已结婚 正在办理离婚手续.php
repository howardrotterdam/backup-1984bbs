<strong>结婚：四年夫妻生活很开心</strong><br />
记者会原定于18：30在香港九展举行，不过还没到18时，已经有过百位记者涌入会场，现场一度混乱。<br />
晚上18：35，郑中基与蔡卓妍同时步出会场会见记者，神情凝重。郑中基首先开腔，承认于06年时确实与阿sa在洛杉矶结婚，而且是在征得双方家长同意的情况下，双方作出的开心决定。至于隐瞒结婚事实，郑中基表示：”我们双方都觉得没有需要、没有必要和大家公开，两位都同意这种处理方法。”<br />
