													<h2>采访全文：郝劲松 以公民的身份作战</h2>
						
						
																											<div id="postmessage_318158" class="t_msgfont">郝劲松，山西忻州人，曾先后7次提起带有公益性质的诉讼。因在火车上用餐、购物索要发票未成，在4个月内连续3次把“铁老大”告上法庭，并最终由北京铁路法院判决郝劲松胜诉。铁道部向全国各铁路局发出《关于在铁路站车向旅客供餐及销售商品必须开具发票的通知》，结束了国内火车上用餐购物不开发票的历史。因打破霸王条款被新浪网和《环球财经》杂志联合评选为“构建经济和谐十大受尊崇人物”；被先后提名为2005中国法制新闻人物、2005年度十大法制人物。他在2005年经手的两起案件分别入选2005中国十大案件、2005中国十大影响性诉讼，并以“维权战士”的身份写入《2005中国法治蓝皮书》。因介入推动铁道部春运不涨价案件入选《2006中国法治蓝皮书》，并当选2006中国十大消费维权人物，2008年被《南方人物周刊》评选为“中国魅力人物 ――公义之魅”。<br />
<br />
本刊记者&nbsp;&nbsp;蒯乐昊&nbsp;&nbsp;发自上海 <br />
<br />
 <strong>我是一个自由人</strong><br />
<br />
人物周刊：网上对你攻击最多的是你的律师资格证，有人说，你连律师资格证都没有，是伪律师。 <br />
<br />
郝劲松：我本来就不是律师，我没有资格证，也从未假装过我有资格证。我是一个公民，一直以公民的身份作战。因为如果你是一个律师，在一些事情上，司法局和你所任职的律所就有办法让你很难受，你要受指挥，你会有顾忌。正因为我没有单位，没有人可以指挥我，我是一个自由人。我需要自己做主，不喜欢被任何力量控制或者要挟。而且我会因此成为一个很好的例子，就是，不是律师也照样可以拥有法律的武器。 <br />
<br />
人物周刊：你现在代理的这些案件中，代理人怎么称呼你？ <br />
<br />
郝劲松：随便，比如张晖就叫我“郝哥”，孙中界叫我“郝律师”，网上有个帖子说，老百姓觉得律师就该是像你这样的人。 <br />
<br />
人物周刊：中国是个讲究“名正言顺”的国家，没有律师执业许可对你开展工作有影响吗？你的收入来源是？ <br />
<br />
郝劲松：我是三家公司的法律顾问，收取顾问费，这工作不会占据我过多的时间精力，所以我有足够的时间来介入公益事件，无偿提供法律援助。我每天都用很长时间关注国内外互联网上的事件，然后选择目标出击，看这个战役，是否适合我。比如邓玉娇案件，我觉得那案子不适合我，因为我不是律师，就无法进到看守所里会见当事人。 <br />
<br />
人物周刊：你似乎从小就是个“刺头”，总是有人会很慷慨地把“讼棍”、“刁民”这样的词汇送给你。 <br />
<br />
郝劲松：凡有上级有管理者，我都试图去改变他，改善他，如果他不服，我就和他拼一拼。包括这次“钓鱼案”，我们去区政府要求旁听，有一个局长说：你可以去接待室喝点茶水，参观一下我们的建筑。我说如果不能旁听就算了，我对建筑不感兴趣。过一会儿他的手下来了，说：郝先生你站在这儿也不太好，都是上访的人，你去接待室坐一下吧，配合一下。我说：我没有义务配合你，我是一个不服从的公民。 <br />
<br />
<strong>网民就是我的狼群 </strong><br />
<br />
人物周刊：实际上，早在2004年起诉地铁的时候，你对于将来战役往哪里打已经有一个大的规划了？ <br />
<br />
郝劲松：是的，从那个时候就已经开始了，我们首先选择地铁这个企业，有政府色彩又是公用服务的企业。在此以前，中国没有发票管理，我们要填补这个空白，为了上厕所的发票打官司，我们当时就觉得法院和政府都会支持，因为发票涉及到税收，我们向地税局举报他们偷税漏税，后来罚了他们2万多，所以第一次就把他们打败了。 <br />
<br />
人物周刊：老百姓看到你索要公厕发票的案子，会不会觉得这个人怎么这么无聊？这么小题大做？他是不是就想沽名钓誉吸引眼球？ <br />
<br />
郝劲松：无聊？我们会告诉他：一张小小的发票，浓缩了你的权利，这是最简单的东西，你可以扔掉，但是里面凝聚着法制和民主的力量。发票背后有国务院发票管理办法，法规被践踏了你都没有维护，今天你失去了发票的权利，明天你失去了其他，总有一天你的房子、你的地什么都失去了，因为法制在被践踏的过程中你从没有维护过它。 这个世界所有的不幸其实和我们每一个人密切关联。就像美国波士顿那个犹太人纪念碑上神甫所说的那段话：起先他们屠杀犹太人，我不是犹太人我不说话，后来他们屠杀共产主义者，我也不是共产主义者，我也不说话。最后他们提着刀冲我过来了，没有人替我说话，因为大家都倒下去了。 <br />
人物周刊：你把每个案件都当成一场战役来打，特别注意战略战术，我想你一定是军事爱好者。 <br />
<br />
郝劲松：非常爱好！我出生在军人家庭，我父亲就是一个老军人。我从青少年时代起研究了大量的战役，尤其是二战，包括海战、陆战，但我最喜欢研究狙击：一个很小的个体力量，有时却可以阻挡一整支军队的前进，甚至成为扭转战局的关键。 <br />
<br />
人物周刊：你有自己崇拜的军事家么？ <br />
<br />
郝劲松：拿破仑！拿破仑在土伦城堡的时候，集中排炮对着国会大厅，以前大家从没遇到过这样的打法，把所有的重炮都集中起来。他整体的战术是非常干净利落的，他自己概括的就是：“不玩弄太复杂的伎俩，集中精力各个击破。”我觉得这句话对我处理很多事情都有巨大的影响。所以我们能取得成功，我希望我们的名字能被刻入法制的重要历程。 <br />
<br />
人物周刊：你说的“我们”是指？ <br />
<br />
郝劲松：我说“我们”，有的时候也是我为自己壮胆，这样一说，就好像有这样一个团队、有一个集体，“我们”包括很多网民，很多民意的力量，共同关注、介入一些公共事件。我有时冲在前面，像一杆标枪一样往前扎，是巨大的网民在我的背后把我使劲儿向前推。 <br />
<br />
人物周刊：可曾有过这样的感觉：你以为有人在背后支持你，推你，可是真的发生什么动真格的事情，你回头一看，身后没有人，你一个人在孤军奋战。 <br />
<br />
郝劲松：迄今为止我还没有这种感受，每次我身后的人都巨多。我研究二战时候的一些战役。德军为了打断盟军，就开展“狼群战术”，在大西洋分布了上百艘潜艇，神出鬼没，击毁了大量盟军的运输船，盟军也采取了相应对策。这种“狼群战术”有时是非常管用的，网民就是我的狼群！ 我需要召唤到大量的人介入公共事件，并让大家明白，对不平之事，你的反抗是有效的，只要你用适当的途径、适当的技巧、适当的尺度。就像古装片中的城门，坚固，高大，攻城的人群拿粗壮的圆木用力撞击，门固若金汤，但只要我们有足够的人，不停地撞，使劲撞，虽然门还没倒塌，但从微观物理上来讲，它已经在慢慢变化了。 每一座城池都是这么攻下来的，每一个网民的留言都可能成为攻陷城门的最后一次撞击。我需要网民在这些战役中成熟、成长起来，使网民认识到我们没有沉沦，我们可以参与决策进程，并在社会正义的关键时刻站出来力挽狂澜。 <br />
<br />
<strong>挥舞着法律的斧头 </strong><br />
<br />
人物周刊：中国现在法律圈里，有你特别佩服的人吗？ <br />
<br />
郝劲松：江平算一个，他是一个高山仰止的人物，但是他比我们年龄大太多了，现在也很少出来发言，他说想把机会让给年轻人。另外，张思之当然也算一个，我们第一次见面，他知道我，还搂着我照了一张相，他给我一个评价，“我是老好事之徒，你是小好事之徒。” <br />
<br />
人物周刊：张思之大律师是把你引为同类了？ <br />
<br />
郝劲松：对，我想最起码，他是知我的。他绝不会仅从某个个案来了解我，以为这个人很无聊，就是为了一张车票或一张发票在四处告状，我想张律师能解读到这些案件背后的深意。 法律一直是我的向往，治理国家最终我们依靠的是法律，法律就是我的工具，我的斧头，当这把斧头磨得锋芒毕露的时候，我们就试着挥舞它，在试验它并使用它的时候，我们也赋予它力量。 <br />
<br />
人物周刊：我们通常都说“正义之剑”，为什么你说“法律的斧头”？ <br />
<br />
郝劲松：斧头更像我的打法。剑那玩意儿很长，它的招式就比较传统、正规，舞剑必须遵循一定的套路，对我来说，剑术有时也是一种规则的束缚，而斧头则不然，斧头更灵活，它没有固定的章法可循。 <br />
<br />
人物周刊：在你挥舞斧头的过程中，这个斧头是不是也会伤到一些人？ <br />
<br />
郝劲松：伤！绝对的。比如说铁路发票那个案子，我们起诉了3次，第三次当我们掌握到了足够白条（证据）的时候，铁道部其实很紧张了，因为他们感觉就要败诉了，涉案的两个车长到处找我，希望能通过说情让我撤诉，后来我听说那两个车长被免职了，被贬到货运列车了。<br />
 <br />
人物周刊：也许有些人因为你，命运就被改变了。 <br />
<br />
郝劲松：一个时代的进步，在它往前走的时候，总会牺牲一些人，如果这些人的利益很小，牺牲就牺牲了，在所难免。 <br />
<br />
人物周刊：所以你并不顾惜他们？ <br />
<br />
郝劲松：不顾惜！那个证据对我非常重要！他只是被免职，而且据我所知，几年后他又官复原职了。我觉得这种牺牲很小。 <br />
<br />
人物周刊：媒体也是你每场战役的外援，你似乎特别懂得调动媒体，什么时间点，放出什么样的消息。 <br />
<br />
郝劲松：我已经习惯了不断地接受采访，回答相似的问题。张晖一开始觉得很麻烦，对他的日常生活是一个干扰，我告诉他，你每接受一次采访，我们胜诉的可能就增加1%。最初他很悲观，认为胜诉的几率很低，我说，即使落败，也要把对方送到被告席上。原告只需要掏50元，但是政府要请律师需要几千元，如果大家都有法律意识，政府的违法成本就会巨增。诉讼有时是有效地缓解愤怒的渠道，社会总得有个泄洪口。 <br />
<br />
人物周刊：如果你现在需要媒体帮助，你能调动的媒体资源能有多少？ <br />
<br />
郝劲松：其实也不会超过20家，这20家还是有些能登出来，有些登不出来，我手头掌握联系方式，以及采访过我的媒体记者大概有150多位。 <br />
<br />
<strong>民主还是一个孩子 </strong><br />
<br />
人物周刊：听说张晖考虑过退缩。 <br />
<br />
郝劲松：我一开始就跟张晖说，代理你的案子，我可以一分钱不要，但我就一个条件：坚决不能撤诉！一、二审的司法程序必须走完，拿到判决书。哪怕最后你败了，民众也会知道到底谁对谁错，也会让他们通过败诉判决知道一些问题，比如法院并不是永远正确。 <br />
<br />
人物周刊：为何你如此坚持不能撤诉？ <br />
<br />
郝劲松：张晖有很多顾虑，如果他顶不住，撤诉了，我们就前功尽弃。张晖在接受采访时提到自己很害怕，害怕钩子报复等等。我告诉他，这种信息你不能释放出去，对手知道你怕什么，就会用你害怕的方式来对付你。 经过这些事，他也在成长，他有时候会长时间地坐在电脑前，看网友给他的留言支持，觉得自己并不孤单，很强大。他从一开始不愿意露面，不愿意暴露真实姓名，用了个化名“张军”，到最后愿意接受凤凰卫视采访，把“张军”改回原名张晖，他说：我突然觉得我又没做错事，我为什么怕他们呢？ <br />
<br />
人物周刊：现在上海政府已承认在张晖和孙中界的事件里确实存在非法取证，张晖也领回了自己被罚的款子，要他抵制压力坚持继续诉讼，是不是很困难？ <br />
郝劲松：除了张晖和孙中界，还有大量的“钓鱼”受害者得不到公正的裁决，我们还没有真的折断钓钩以及钓竿。张晖去领罚没款时，闵行区建交委门口聚集了很多围观者，人群中有人对他喊，“你的钱领回来了，我们的钱还没。” <br />
<br />
张晖是一个受过良好教育、收入相对富足的中产阶级，他被“钓鱼”后非常愤怒，首先通过论坛发文给媒体，行使了自己言论自由表达的权利，他正是我需要的一个人成长为公民的标本：一个温文尔雅的知识分子，通过这个事情开始反抗。 <br />
<br />
孙中界是另一种类型：小孙是刚满18岁的农村青年，从没受过城市工业化、机械化生活的伤害，他对大城市有美好的向往，离开家乡来到城市工作，到上海的第三天就遇到“钓鱼”，他整个人崩溃了，城市的形象在他心里倒塌了，他觉得城市里的人不应该这么坏，他想不通，这促使他操起了菜刀。这种行为是有血性的，一个民族有血性，这个民族还有希望。中国现在烈性男儿太少，遇事不敢反抗，看到别人出事也很冷漠，觉得和我无关。不作为的公民太多，他们根本称不上公民，没有法律意识，没有监管政府的意识。很多人觉得我只是个体，胳膊拧不过大腿，我们就要改变这种传统想法。 <br />
<br />
社会仿佛人之肌体，有些病症，你应该不断去暴露它，病灶才会被及时发现、被医治。如果你觉得司法体制黑暗，你就不去使用它，它会越来越烂，等到做手术的时候也没用了。公民如果没机会成长，剩下的就是一群暴民。谁也不愿看到社会动荡，谁也不愿看到天下大乱。民主是一个孩子，它有个成长过程，你得培育它，为它的成长准备条件，一个健全的社会应该懂得培养它的公民。 <br />
<br />
人物周刊：你觉得你的方法有效吗？ <br />
<br />
郝劲松：成长不能靠说教。不知你注意到没有，从2004年到2009年，全国各地的法院接到大量官司，如果有人能调查到这个数据，会发现这是个剧增。我和北京市高院的法官聊天，他们星期天都加班，因为案子暴增，法院增加人手的速度，追不上案子增加的速度。 <br />
<br />
某种程度上，我觉得这是个好事儿，公民试图上法院去告状，就说明他有了法律的意识，并且迈出了行动。我看到许多人打官司我就很兴奋，我知道那里头有我，有我的功劳，以及我的同类。</div>

							
							
							
							
