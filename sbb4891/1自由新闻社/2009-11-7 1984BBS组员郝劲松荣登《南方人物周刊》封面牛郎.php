艾未未：思想自由<br />
<br />
艺术家<br />
<br />
我梦想能活着的时候实现自由，在通过黑暗甚至令人窒息的隧道，都能达到光明的彼岸。<br />
<br />
 <br />
<br />
ESQUIRE:您已经实现了自由了吗？<br />
<br />
任何一个活着的人都不可能实现。自由只能是一种愿望，对现实处境超越的一种想象。人是非常有局限性的，自由就是局限之外的东西，就像夸父追日。<br />
<br />
ESQUIRE:你为自由都做了什么？<br />
<br />
可能和我的职业身份有关，我是一个所谓的艺术家，是一个表达者，一定是要突破局限的。<br />
<br />
我从一个搞艺术的人，到一个酷爱写博客关心通俗问题的人，我新的表达方式，就是互联网和博客。<br />
<br />
我以往的表达体系是几百年来的文艺复兴以后对文化的定义。你的东西必然要为权贵所欣赏，一定要挂在画廊……有些类似于宗教仪式，跟更广大的现实关系很淡。<br />
<br />
我没学过建筑，对建筑的意见是出自伦理和价值的判断上，大部分的建筑师的理念是建立在一种理工学校的技术学习的基础上，没有审美的教育。<br />
<br />
比如说鸟巢，我希望它是开放式的，形体感觉没有中心，从任何角度看，都是正面，没有遮掩，结构就是外观。这就是一种观念的产物。<br />
<br />
ESQUIRE:如果你裸奔，如果你对警察说你是行为艺术家，那么你可以不被当成精神失常者对待。但同时当别人能容忍艺术家种种出格行为时，艺术家所争取到的创作权，就成为一种被边缘化的境遇。<br />
<br />
被边缘化就不存在真正的自由，当艺术家只是享受作为异类而得到的特权时，对社会的影响非常有限。只有艺术家和普通人的生活建立广泛的理解和情感的交流，它的作品才真正富有含义。只有当作品提供对文化和凡夫俗子的日常思考建立某种关系时，人类的共识才可能表达，人与人之间是有联系的，而非隔离的，如果人们普遍处于不联系的状态，那么情感是被切断的，你的死亡是别人的快乐，你的痛苦是社会消遣的商品，你的失去正是他的获得。<br />
<br />
ESQUIRE:自由是青春的分泌物吗？<br />
<br />
青春和自由有很大关联，青春不需要教义，生命本身已是完整的系统，教育只是附加。生长本来就是和变异和分裂有关，建立在破坏基础上，建立在若干错误之上。青春期的自由又是单纯的，遇到的矛盾相对简单，但当你到了成熟期，面对复杂的矛盾，那种生物性的自由就要转化为另一种可能。<br />
<br />
ESQUIRE:你个人的自由观是如何形成的？<br />
<br />
我幼年时家从新疆的一个边陲小镇搬到更荒凉的戈壁滩时，我记得自己从车上下来时，就在戈壁滩上狂奔，一直跑到瘫倒在地，我从来没见过这么空旷的地方DD无边无际的沙石和天空。那时的我真的有一种离开人群，获得释放的强烈愿望。<br />
<br />
在我出国之前。当我坐着飞机在纽约的上空盘旋，等待降落，看到一片灯火，像是熔炉的钢水一样流出来的时候，你会感觉到人类的力量和欲望，自由空气的刺激。当然，在美国我只是体验到了自由的一部分。我认为美国人非常不自由，尤其美国的中产阶级，是奴隶一般生存，或者说是像流水线一样生活，价值观和行为模式几乎都是一个模板制造出来，思考范围和个人意识都相当狭窄。美国梦就是一个典型的催眠道具，即使在以自由著称的美国，人也很难真正地文明化。我感到最大的自由，就是认识了互联网。博客和互联网让人类的自由得到了最大化DD不依附任何结构，只依附一个技术条件，就能够成为人。我敢断言：互联网以后，是另一个新的人类。互联网将重整所有的语言体系和权利结构，<br />
<br />
德国的《时代》周刊约我撰文，邀请我写对美国自由观的看法。我认为这次经济危机是旧体制崩溃的前奏，危机是由互联网引起的，和过去相比，幕后交易因为网络的存在而变得困难了。有了互联网，瞬间就知道过去几天才知道的的消息。经济危机是美国式自由观的破产。它鼓励了个人的欲望绝对化，可以凌驾于社会，这种绝对化是不可能长久的。<br />
<br />
ESQUIRE:你怎么看东方人的自由观？<br />
<br />
我们有国有家，三纲五常，仁义礼智信，就像中药一样分门别类，使每个人都在格局中，各安其命。但我们不是一个宗教的社会而是伦理的社会，但当离开了这个体系后，就面对一个没有结构的社会。现代中国缺少阶层和层次，不丰富，所以很危险。你看官方的语言是这样表述的：一抓就死，一松就乱。中国社会的模式不是多元的，所以它的弹性差，而真正的自由状态就像热带雨林，所有植物都可以在其中各得其所，中国这块盐碱地需要很长时间重新滋养丰润。<br />
<br />
ESQUIRE:中国文人的自由观是怎样的？<br />
<br />
中国文人挺奇怪，为了获得更大的自由，而追求权力，但依附权力时，他们非但没法改造权力，反而成了权力的附庸或者说代言人。不像西方知识分子那样独立批判和不合作，自由是没有前提和假设，没有先决条件，更不能用不自由的手段达到。<br />
<br />
中国古代人对自由以及一切概念的看法就是和谐和美满，最终一定要是好莱坞式的大团圆结局，认为自由是融合而产生的；西方人看待的自由更多是抗争和分裂的，生命其实就是细胞分裂产生的，于是产生了进取和侵略性的自由观。<br />
<br />
东方人的自由观是否需要被西方人的所改造？<br />
<br />
我认为东西方的观念是前互联网时代的产物，互联网已把这个世界变圆，没有方向感。你可以看到奥运会不只是中国人的奥运会，而也是麦当劳和可口可乐的，连转播权都是NBC的，无论是利益的体现还是表达和诉求，都是和普通人都超乎想象地紧密联系在一起。<br />
<br />
ESQUIRE:自由是一种必需品吗？<br />
<br />
就像被卖掉了还帮别人数钱。也许自由的确不是必需品，直到他们看到了人的另一种生活的可能性。<br />
<br />
自由就是更新，解放就是进化，这是生物规律，在一个常年不发生更新的社会里，你不知道缺少什么，就像一个农村孩子喜欢在烂泥塘里滚，如果你把他带出来，他还不愿意，那是他最大的幸福。但他有没有权利拥有更多的选择？自由的终极意义就是信息，就是表达和获得，输入和输出，为什么我们害怕信息？因为更多的信息会造成更多的需求，更多的需求会造成更多的不稳定。我们的稳定是建立在对他人自由的定义上，通过对人的潜能的限定，这其实是一种悲剧。人类迈出的每一步都和克服自身的恐惧和约束有关。<br />
<br />
公民社会不是什么高深的理想，就是一些基本的原则。比如人都有吃饭的权利，有说话的权利，有听到和被听到的权利。有时实现你的理想就像出生，需要通过一个黑暗的非常困难的甚至令人窒息的隧道，才能达到光明的彼岸。<br />
<br />
自由就是人之常情都有所归宿。</div>

							
							
							
							
