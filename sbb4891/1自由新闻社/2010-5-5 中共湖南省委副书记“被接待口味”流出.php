																											<div id="postmessage_598009" class="t_msgfont">公务接待工作是面子工程<br />
<br />
・潘柳清<br />
<br />
公务接待工作是党委、政府机关工作的一个组成部分，其重要性是不言而喻的。近日一位省领导指出：公务接待简扼地讲就是一项面子工程，公务接待工作就是面子工作。这一概括暨精辟又直白，包含了丰富的内涵。<br />
<br />
中国是礼仪之邦，中国人最讲面子，也最爱面子，且事事争面子，有时死要面子，这实际上映照出一个民族的自强不息好胜进取求荣的精神。在乡下自家盖房子，要办几桌酒席，请左邻右舍七姑八姨来庆贺，以示人丁兴旺装点门面；单位乃至市县要搞一项建筑，往往都会强调要做成标志性建筑，或叫做面子工程；人们出差办事或走来访友，里面穿旧的或差的，好衣服穿在外头，这叫装体面；抽烟的人大多口袋里装两种烟，自己抽差的，好烟装给客人抽，这叫装面子；帮人解困、全力相助，这叫给足面子。接人待物更是如此，远方的客人来了，亲朋好友来了，主人不亦乐乎。以最诚挚的心态，由衷的高兴和满腔的热情来迎接。往往是杀鸡宰鸭、沽酒称肉、毫不吝啬地倾其所有。如果自家没有，到邻居家借，到店铺去赊，也要做出最好的菜肴来招待客人。这样才能给客人以面子，主人有面子。<br />
<br />
公务接待工作是做面子工作，不仅有本民族的历史渊源，也是现实意义使然。上级首长要来视察工作或探亲访友，兄弟市州的领导要来进行工作访问。公务接待部门要以浓烈的地域欢迎仪式来迎接客人，要把本地域或本单位最美好、最靓丽、最具特色的一面呈现给客人，这叫面子上的工作。她直接或间接地反映出本地域或本单位的精神文明建设、经济建设、人文地理、人与自然与社会的和谐程度。下榻地要选择本地最好的酒店，宴请时要上几道最具地域特色的菜肴，即主菜也叫面子菜。礼品要包装精美，既赏心悦目又经济实惠，且要带有地域特色。<br />
<br />
公务接待工作是做面子工作，就是要为本地域或本单位争面子，为领导争面子，要给足客人以面子。达到尊重客人、联络感情、交流信息、引资招商、推动本地经济发展之目的。公务接待中给足客人以面子，就是实现全方位的优质服务。这与相互攀比，形式主义是不能同日而语的。在“简化礼仪、务实俭节、杜绝浪费”的基础上做好面子工作，是公务接待工作中的重要任务。</div>

							
							
							
							
