<br />
8月30日上午，薄熙来、黄奇帆一行来到万州工业园区，先后考察了密胜（重庆）科技有限公司、长安跨越专用车有限公司、谭木匠工艺品有限公司和万州三峡服装艺术学校。每到一处，薄熙来、黄奇帆都非常关心这些企业和学校对就业的拉动作用。薄熙来说，承接加工贸易梯度转移、鼓励创办微型企业、做好职业培训，都是创造就业岗位的好办法，一定要抓好抓实。<br />
<br />
“老人家，这个广场怎么样？每个礼拜能来几次？”看到一位白发苍苍的老人正在锻炼身体，薄熙来停下脚步问。老人中气十足：“喜欢得不得了！地方大，环境好，空气也好，我每天都来这里锻炼！”薄熙来对市规划局长扈万泰说：“广场也是民生工程，在有条件的地方，要尽可能建得大气些，漂亮些，让老百姓舒心、满意！”<br />
<br />
　　广场一角，由一群老师和同学组成的红歌队正在放声歌唱，看到薄书记来了，大家围拢过来。有人提议和薄书记一起唱一首《长江之歌》，薄熙来欣然应允，大家纵情高歌。一曲唱罢，薄熙来对合唱队员们竖起大拇指说：“看到你们年轻人唱红歌我就高兴，在长江边上唱《长江之歌》就更有味道！”他还对大家说：“时间过得很快，我们也上岁数了！看到你们这些年轻人，就让我想起毛主席那段有名的话，你们还记得吗？”在场的人一齐大声说：“记得！你们年轻人朝气蓬勃，就像早上八九点钟的太阳，希望寄托在你们身上！”广场上一阵爽朗的笑声。<br />
<br />
另一边，“老朋友合唱团”正唱着《保卫黄河》，歌声激越。看到薄熙来一行，大家很兴奋，热情地拉薄熙来加入他们的行列。听完大家的演唱，薄熙来说：“我老家是山西，就在黄河边上，大家刚刚唱了《长江之歌》，现在又要《保卫黄河》，让我感觉特别亲切！涪陵人民的精气神很足，希望大家一起努力，把长江边上的涪陵建设好！”他还把涪陵区委书记张鸣和代区长沈晓钟拉到大伙儿面前问：“这两位大家认不认识啊？”大家都认识张鸣，沈晓钟刚到任十几天，不少人还不熟悉，薄熙来便现场介绍，还一脸严肃地对大家说：“他们两位干得好，大伙儿就给他们鼓掌；要是干得不好，你们就来向我告状！”现场又是一片欢笑。<br />
<br />
在两区调研时，薄熙来十分关注民生。他多次叮嘱两区的党政负责人，一定要把民生问题落到实处。<br />
<br />
　薄熙来说，对困难儿童、群众和残疾人，一年有四个节日都要照顾好，端午节要送粽子，中秋节送月饼，国庆节送时令食品，春节包饺子，每逢节日都要让困难群众好好改善伙食，对这件事要不厌其烦。党政干部决不能嫌贫爱富，为穷人办事是共产党和人民政府的本分，感情一定要到位。“大下访”就是党和政府密切联系群众、改善机关作风的有效途径，一定要坚持下去。<br />
