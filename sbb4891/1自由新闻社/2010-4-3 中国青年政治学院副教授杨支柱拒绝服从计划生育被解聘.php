													<h2>中国青年政治学院副教授杨支柱拒绝服从计划生育被解聘</h2>
						
						
																											<div id="postmessage_546790" class="t_msgfont">杨支柱按：上周日跟几位老朋友一起吃饭，说到萧瀚下课一事，我说我也下课了，因为老婆生了第二个孩子，但我没拿到书面通知。到了周日下午，就有自称“自由亚洲”电台“冯日遥”的女士给我打电话问了我许多问题，我回答了一部分。今天看到这篇报道，觉得大体上差不多，但少了一些我的话，尤其是加进了一些别人的东西之后，让人觉得我是二胎论者。我特此声明，我不是二胎论者，我的自由生育论者。电话采访未经核实，我口音又重，细节上的出入我就不挑了。<br />
&nbsp;&nbsp;<br />
<br />
作者：冯日遥&nbsp; &nbsp; 文章来源：RFA&nbsp; &nbsp; 点击数： 52&nbsp; &nbsp; 更新时间：2010-4-1<br />
&nbsp;&nbsp;<br />
中国青年政治学院法律系副教授杨支柱，因拒绝服从“一胎化”的计划生育政策，妻子于去年底产下第二胎后，他近日收到被校方解聘的口头通知，杨支柱表示对增添家庭成员没有后悔。大陆实行“独生子女”政策30年，有学者认为有必要考虑放宽。（冯日遥报道）<br />
<br />
被解聘的杨支柱以生育二胎挑战国家政策表示无悔。杨支柱周三接受本台记者访问时指，从妻子再次怀孕后起，学校及居委会不断派人找他，向他施压要他放弃孩子，但他一直坚持将孩子生下来。妻子于去年十二月产下第二名女婴，直至上周五，杨支柱收到校方口头通知要解聘他。他说：之前我还一直有回校上课，但上周五校方口头通知要解聘我，指我生下第二胎，违反计划生育政策，要被记大过，要处分，不准我上课。<br />
<br />
杨支柱说，早已作好被解聘的心理准备，现时他正要求校方发出书面解聘书，对于即将离开工作近6年的工作岗位，他表示不感到特别可惜，日后他会撰写文章及写书维持生计。<br />
<br />
大女已经三岁多的杨支柱说，因为超生被罚款20多万元，他认为罚款是毫无根据及极不合理，所以拒绝缴交。对于女儿面临无法上户口，甚至无法上学，杨支柱重申不会后悔。他指，独生子女政策存在争议外，执法人员的野蛮做法更令他气愤难消。他说：农村的计生办人员执法太恐怖了，他们会毁掉超生家庭的房屋、捆绑超生妇女去卫生院结扎等，生育要得国家准许证，超生被罚巨款，所谓的社会抚养费，都是没有道理的。<br />
<br />
本台曾就杨支柱获口头通知解雇的事向校方查询，办公室工作人员著记者直接向杨支柱任教的法律系询问，法律系工作人员承认杨支柱已被停职，并指事件不影响学生正常学习。他说：学生的学习未有受到影响，我们系内仍有其他教师的团队成员，不会因为一位教师的停职而影响到他们的学习。<br />
<br />
至于校方何时发出正式解聘书，该工作人员指他不太清楚。<br />
<br />
杨支柱被解雇的消息曝光后，不少网友发帖表示支持，有网友批评独生政策侵犯人权，又指现时中国的出生人口性别比例为118个男比100个女，该比例是严重失调，应该放弃政策。<br />
<br />
清华大学人民社会科学教授胡鞍钢接受本台记者访问时指，大陆现时面临新的人口问题，需要及时调整人口政策，他认为大陆应该尽快采取更加宽松的生育政策。他说：就好似现时上海已实施逐步放宽有关政策，容许同时独生子女的夫妇，可以多生一胎，即为两胎，因为一胎化造成的家庭结构很有问题，一个孩子长大成人后可能要养父母及父母的上一代，负担很大。<br />
<br />
胡鞍钢又说，中国人口增长过快的情况早在上世纪90年代就已发生逆转。进入21世纪后，中国成为世界上人口增长率极低的国家之一，2008年人口自然增长率降到千分之五，中国妇女总和生育率降至1.8以下，属于“少子化”类型国家。<br />
<br />
面对调整人口政策的种种讨论，一些地方已表示正在考虑放宽生育二胎的控制标准，不过国家计生委最近明确表态，5年内计划生育政策不变。<br />
