																											<div id="postmessage_550861" class="t_msgfont">对于政策好坏，可以参考南方周末的《山西翼城：放开“二胎”25年》。<br />
<br />
<br />
<br />
放开“二胎”是不是一定会导致人口增长？山西翼城县作了否定的回答。<br />
<br />
这是一个试点“二胎”政策25年的“人口特区”，其人口增长率反而低于全国水平，各项人口指标优于全国水平。<br />
<br />
这背后的秘密是什么？它的经验，有没有可能影响决策层？<br />
<br />
独生子女政策在一片吵吵嚷嚷中走过了30年。而山西翼城县，却做了一个历时25年的人口试验――放开“二胎”。<br />
<br />
这一鲜为人知的“二胎人口试点”，以“晚婚晚育加间隔”的方式逐渐成型。<br />
<br />
令人震惊的是，25年后，这个试点县的人口增长率反而低于全国水平，各项人口指示也优于全国平均水平。<br />
<br />
为什么是翼城<br />
<br />
“30年前的翼城试点对我来说，是一种无可奈何的选择。”梁中堂说。<br />
<br />
1979年，全国不分城乡推行“一胎化”生育政策，传统中国“多子多福”的生育习惯由此被改变。<br />
<br />
现任上海社科院经济研究所博士生导师梁中堂，其时是山西省委党校教师。他在30年前给全国第二次人口科学讨论会的论文中已经指出：如此激烈的生育政策必将导致中国人口老化。<br />
<br />
在这篇论文中他还认为：当时的人口统计方法有误，科学的统计是在特定的时间点上统计人口数据，并且要统计出男女性别比例，而并非简单的依靠各地公安部的户籍统计。加之计划生育对于农村人口的压缩并不适于我国，同时提出了“晚婚晚育延长间隔”的“折中”方案。<br />
<br />
在被计生委否定5年后的1984年春节，已经是山西省社科院人口所所长的梁中堂急切地向时任中共中央总书记胡耀邦写信，建议试行这一生育政策。信件很快得到了中央高层的批复，并有意选择一两个县试验。1985年，冀城作为中国的首个“人口特区”， 以“不能宣传”为前提得以顺产。<br />
<br />
人口书籍《八百万人的实践》中提出，与翼城试点一并提出的还有三个试点：湖北恩施，河北承德，甘肃酒泉。但事实上，历史上并没有所谓的四个计划生育试点：湖北恩施为土家族苗族自治州本属于少数民族享有可生育二胎权利。承德属于山区囊括在国家计生委提交中央报告的第七条“边远山区和沿海渔区特殊困难户可生二胎”。而甘肃酒泉也并未真正意义上实施过二胎政策。而“女儿户”等二胎试点，均是由下至上争取而来。因此，真正意义上的二胎试点仅为山西翼城。<br />
<br />
梁中堂为什么把试点选在翼城？<br />
<br />
梁中堂认为，“二胎”试点必须符合四个条件：一是典型的农业县，二是该县人口情况清晰，三是县委领导能力强，四是省县领导有较好沟通。最初“二胎”试点并未在翼城县而是长治市高平县。而后来走访到翼城时，梁中堂发现翼城的人口底子很好，人口增长幅度不大且计生数字比较具体。而时任领导的看法也给梁中堂吃了颗政府“定心丸”：“我认为强行推行一胎化是很难的，而在听取了梁中堂和试点的介绍后，我同意他关于人口的算法，我认为是可行的。”时任山西翼城县委书记武伯勤与梁一番谈话后，最终“花”落翼城。<br />
<br />
试点后，人口增长率反而低于全国<br />
<br />
1985年，在山西翼城开始试点梁中堂的“二胎”政策。县政府建议农民生育要有计划，妇女第一胎在24岁生育，第二个孩子则在30岁左右生育，杜绝三胎和三胎以上的生育。梁用这样的方式来检验中国人口出生率是否会因为计划生育政策而有所下降。<br />
<br />
车月莲，从70年代一胎化实行时就在翼城担任当地计划生育宣传员，至2009年退休。她说：“在翼城试点施行前，虽然政策只允许生一个，但农民日常生活导致生育都在两个孩子以上，政策与实际生活的差距，无论是政府还是农民都受到了许多人为的摩擦和障碍。”<br />
<br />
1990～1998年期间担任翼城县计生委主任的冯才山更将当时的计生工作称为“天下第一难事”：“在试点刚刚开始的时候，因为计划生育是国策，最开始大家都以为不能越过这个雷池，对试点的设立感到很惊奇。当时我在翼城龙化镇当镇长，我的第一反应是：我是不是听错了。这一胎化好不容易推展开来，能就这么变了？并且当时还有个担心就是：在放开了二胎之后，农民会不会继续生三胎、生四胎？”冯才山的这些忧虑，日后25年的计生工作中的事实，给出了答案。“一位不到30岁的农村妇女躺在床上一句话也没有，只是用一半是疑问、一半是哀怨和忧虑的眼神盯着我。这个图像一直到现在都十分清晰地定格在我的脑子里。”1988年的事，至今在梁中堂印象里不曾抹去。<br />
<br />
在当地政府、学者和群众的合作下，政策逐渐由不合情理调整为合乎情理。翼城试点在一开始便步入正轨，从刚刚开始一户一户的去进行“家访”，再到后面组织群众开群众动员大会，梁中堂用最简单但却最直接的方式在翼城逐渐将“晚婚晚育加间隔”推广至户。<br />
<br />
施行试点时，翼城有17个乡镇，当时人口就已经27.8万人了，当时的目标是到2000年，人口不超过30万。<br />
<br />
而这个异于全国的人口试点，却在1982－2000年全国两次人口普查期间，交上一份满意的答卷：该县各项人口指标数据均优于全国水平。在此期间，全国人口增长了25.5％，而翼城县放开二胎却仅增长了20.7％。而另一个重要的人口统计指标出生性别比全国为117.8，翼城则为106.1。<br />
<br />
试点经验还没有影响政策<br />
<br />
在谈及翼城30年印象最深的事时，1986年就开始从事计生工作，而现任翼城县计生委办公室主任吴宝堂说：“别的地方生育率都是1.0，1.3，1.5，而只有我们翼城是2.0。这就是让我印象最深刻的事情。”在翼城试点获得成功后，有些邻乡县的女孩子会为了能生二胎嫁到翼城来。“有人说是因为有了我，翼城的老百姓才得以生育二胎，但事实上，是我要谢谢他们，因为正是有了他们，我的理念才得以实践，正是因为他们，才能证明我的想法是对的。”梁中堂对南方周末记者说。<br />
<br />
数据直观的显示出：放开二胎政策不仅不会导致人口的迅猛增长，相反，人口增长速度会降低。<br />
<br />
“二胎政策是老百姓的生育意愿和国家政策的最佳结合点。”采访中，一位翼城现任计生干部如是说。<br />
<br />
对中国计划生育政策施行的30年间产生的负面影响，梁中堂不遗余力的四处奔走以寻求调整政策，但均告无果。“翼城我现在已经很少去了，我要做的是将那段历史记录下来。”梁说。<br />
<br />
2009年，梁中堂与易富贤发起了人民网“E两会”上号称“第一提案”的347号提案――《停止计划生育，恢复人口可持续发展能力》。这个提案得到多达4万人支持政策。今年两会，关于调整计划生育的呼声仍不绝于耳。<br />
<br />
30年前梁中堂就对“一胎化”政策会导致的影响进行过预判：人口老龄化，男女比例失衡，四二一家庭结构导致的压力正逐一的变成现实，被众多体制内外的学者反复研究并讨论，其中包括了当年的强硬派宋健、田雪原等人。但人口政策决策层对翼城县的试点尚无明确表态。<br />
<br />
梁中堂感慨：“历史实际上是一股大势，而这是由民众构成的，我们能做的，也不过是在几十年人们谈及这问题时，会发现有我们在提出反对意见。”</div>

							
							
							
							
