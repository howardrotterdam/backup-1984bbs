A30 |&nbsp;&nbsp;观点 |&nbsp;&nbsp;<br />
<br />
By 严家祺&nbsp;&nbsp;2009-04-29<br />
<br />
20 年后看「四?二六」社论&nbsp;&nbsp;<br />
 <br />
「四・二六社论」的形成是4 个因素同时起作用的结果。这4 个因素是：一、陈希同、李锡铭「谎报军情」，对学运作出<br />
<br />
夸大其词的报告；<br />
<br />
二、赵紫阳在学运初期访问朝鲜；三、李鹏取代赵紫阳的野心，趁赵紫阳访问朝鲜时主持政治局常委会，作出「动乱」的决定；四、邓小平的专断独行。<br />
<br />
1989 年4 月24 日，北京市委邀请教委正副主任李铁映、何东昌等参加高校负责人会议，表示应当旗帜鲜明地对待悼念胡耀邦的大学生。下午，以李锡铭、陈希同为首召开的北京市委常委会会议一致认为，学生悼念胡耀邦的行动已经发展成大规模的「闹事」，矛头直指党中央，妄图推翻共产党的领导。这是长期以来「资产阶级自由化」思想泛滥，地下组织、非法组织猖狂活动，党内外、国内外敌对势力勾结的结果。向中共中央建议，必须旗帜鲜明地对当前事态表明态度；报刊等宣传舆论工具，必须坚决地服从中央的统一指挥，大力配合，造成强大的舆论与声势，扭转当前的被劫局面；授权北京市委、市政府放手发动群众，组织反击，打退反动思潮的进攻；望党中央、国务院尽快拿出几条过硬的措施来。<br />
<br />
「谎报军情」获邓小平青睐这时，赵紫阳正在朝鲜访问，4 月24 日晚，李鹏主持召开了政治局常委会会议。虽然几个常委都知道赵紫阳反覆强调的3 条意见，但是，受北京市委夸大其词的报告及李鹏的影响，会议认为，当前的种种迹象表明，在极少数人的操纵下，一场有计划、有组织、反党反社会主义的政治斗争已经摆在面前。决定成立中央制止动乱小组；要求北京市委和市政府尽快平息动乱，稳定局势；责成《人民日报》发表社论，指明这场斗争的性质。<br />
<br />
25 日上午，邓小平在家中听取了李鹏、杨尚昆、乔石、李锡铭、陈希同等人的汇报，李鹏、陈希同除了夸大其词渲染极少数学生和民众的激进行为，诬说学生结成非法组织、四处募捐、全国串联，一场全国规模的包括中学生、包括工人在内的大规模行动正在组织发动之中；还有意特别夸大对邓小平个人的言论，说有人公开喊叫「打倒邓小平」，俨然一场「文化大革命」式全国上下打倒当权派的群众运动即将扑面而来。邓小平当即赞同他们的分析和决策，表示，这不是一般的学潮，是一场否定共产党的领导，否定社会主义制度的动乱。必须旗帜鲜明地反对动乱，发一篇很有分量的社论，坚决把动乱压下去， 「快刀斩乱麻」。<br />
<br />
北京市委「谎报军情」受到邓小平的青睐，更加得意猖狂，积极为严厉镇压作舆论准备。而原定下午市委参加的清华学生与国务院国家教委对话因学生爽约而流产。李鹏则迫不及待地决定把上午邓小平会见他们时的内部讲话向各级干部传达，有意把邓小平捅了出去。<br />
<br />
4 月26 日，按照邓小平讲话精神炮制的《人民日报》社论〈必须旗帜鲜明地反对动乱〉于25日晚间在新闻联播节目中播放后，出现在全国各大报章头版头条位置。<br />
<br />
「四・二六社论」俨然是一篇言辞激烈的大批判文章。什么「极少数别有用心的人」、「夺权」、「抢占」、「搞散人心，搞乱全国，破坏安定团结」、「是一场有计划的阴谋，是一场动乱」，这些污言秽语一股脑儿地栽到了学生和民众头上。<br />
<br />
学生们最难容忍的是把他们光明磊落的正义行动定为「动乱」，加上北京市公安局宣布，禁止街头募捐，游行是非法行为，使业已怒不可遏的青年人热血沸腾。北大校园内立即出现大字报「必须旗帜鲜明地驳斥人民日报社论」，斥责社论「一派胡言」，告示天下「民心不可辱，士气不可欺」。<br />
<br />
如何看待「四．二六社论」是1989 年「天安门事件」的关键，后来的学运和天安门广场的绝食，主要目标就是否定「四・二六社论」。<br />
<br />
邓小平坚持不容改变「四・二六社论」对「动乱」的定性和一意孤行，最后酿成了「六三六五大屠杀」。（2009 年4 月22 日，写于纽约）作者为中共前总书记赵紫阳的智囊团成员，六三六五时被指为幕后黑手，六三六五后逃亡海外，现居于<br />
<br />
美国</div>

							
							
							
							
