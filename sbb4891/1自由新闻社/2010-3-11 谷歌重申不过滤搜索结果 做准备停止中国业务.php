																											<div id="postmessage_514309" class="t_msgfont">作者： 林涛 中国企业家 <br />
CNETNews.com.cn <br />
2010-03-12 18:05:11 <br />
关键词： 网络 <br />
<br />
<br />
　　谷歌将关闭中国网站，拒绝过滤敏感词的Google.com是否会在中国遭到屏蔽尚不可知<br />
<br />
　　【《中国企业家》网站报道】3月11日上午，有谷歌关键词广告客户反映称，接收到来自谷歌大客户部的电话通知，表示Google.cn将于三月底关闭，客户投放在Google.cn的广告会变更到谷歌全球网站Google.com。谷歌同时承诺，不愿意在Google.com上投放关键词广告的客户可以按照流程退款。同时，之前广告投放合同的主体（谷歌中国或其代理商）会统一变更成为其代理商，不过，谷歌中国此前的客户服务团队将会被会保留，而且销售团队的总部将会变更到中国香港。 <br />
<br />
　　随后，《中国企业家》记者就此事电话咨询谷歌中国公关负责人，该负责人表示，目前谷歌与中国政府谈判尚未结束，公关部并未接到关闭Google.cn通知，同时，她表示已经询问过相关业务部门，并无发生通知客户Google.cn将关闭等事情发生。 <br />
<br />
