<br />
　　不过据网友透露，《华尔街日报》对该文进行过修改，此前援引自消息人士的话并非上述内容，而是“谷歌有可能和中国具体部委签署不同的协议。比如，谷歌有可能在北京和其他受中央政府影响较大的地方被禁止从事某些活动，但政府可能对南方省市的业务网开一面。”这一方案遭到网友质疑，认为不符合中国国情，不过此前，境外电视台如星空卫视、凤凰卫视等也获得在中国南方某些地区的落地，但是在北京等广大地区，他们并未获得落地的资格。 <br />
