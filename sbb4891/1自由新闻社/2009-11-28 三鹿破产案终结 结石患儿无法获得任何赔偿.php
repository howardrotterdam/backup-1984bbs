																											<div id="postmessage_347666" class="t_msgfont">专访“结石宝宝”之父赵连海<br />
一<br />
“如果你再不来，我就认为你是给请去‘喝茶’了。”这是“结石宝宝健康关爱之家”网站（简称“结石宝宝之家”）的创建人赵连海给记者开门时说的第一句话，有点黑色幽默。<br />
穿着印有结石宝宝头像、写着“铭记中国9&#8226;11”字样的白色T恤，赵连海在家中由书房改造的工作室里接受了采访。这间工作室地上铺着床褥，平时供赵连海休息，外地受害儿童的家长来京维权时便充当临时客房。<br />
在接受采访的过程中，赵连海把自己的手机电池卸掉了。他解释，长期从事维权活动难免受到各方干扰，经验告诉他必须要谨慎。<br />
<br />
二<br />
十三个月前，当赵连海建立“结石宝宝之家”网站的时候，他并没有意识到这个举动将会彻底改变他往后的生活。<br />
赵连海曾在北京一家媒体干过发行流通的业务，也在北京某机关下属事业单位的管理层工作过，但他说“看不惯单位领导的作法，不愿与其同流合污”，便辞去了工作，“给自己放个长假陪陪家人”。<br />
北京奥运结束后，赋闲的赵连海决定重新找份工作。他相信凭着自己的工作经验，要找一份收入过得去的工作并不难。<br />
2008年9月11日，赵连海还没来得及投简历、面试，国内媒体就曝光了三聚氰胺毒牛奶的新闻。几天后，他的儿子也被查出患有肾结石。<br />
一年多前赵连海发现自己的儿子不怎么长个子，以为是营养不良导致发育迟缓，他决定让孩子多喝牛奶补钙。“每天都让孩子喝几包牛奶，还买了不少酸奶、奶片类的乳制品，本以为是给他补钙长个，”说到这，他转头看了一眼随意堆在书房角落的空牛奶箱，叹了口气，“谁知道竟然补了个肾结石出来，反而害了孩子。”<br />
愧疚夹杂痛心，赵连海意识到许多家长或许也和自己一样无奈自责需要帮助。“为什么不建立个网站把受害儿童的家长都联合起来，相互交流信息？”<br />
这个想法并没有在赵连海的脑里停留很久，儿子查出结石几天后，他建起了“结石宝宝之家”。<br />
建立网站的初衷是帮助三聚氰胺受害家庭自助互助，同时客观、妥善、理智地来解决出现的问题。但这样的网络平台没有得到各方共同认可，“结石宝宝之家”在建立之初的一个月内连续三次被关闭，无奈之下，赵连海将服务器转移到了境外。<br />
但值得欣慰的是，“结石宝宝之家”依然成功地联合了许多三聚氰胺受害儿童的家长并吸引了人们的注意力。尽管网站数次被关闭、屏蔽，还是有不少家长和热心人士用各种各样的方法浏览了“结石宝宝之家”并与他联系；他所创建或加入的受害儿童家长QQ群每天都有不少家长活跃在线，参与讨论或交流信息；主流媒体开始关注并报道“结石宝宝之家”，“公盟”等NGO组织也向家长们提供各种援助……<br />
<br />
三<br />
“中国人在食品中完成了化学扫盲：从白酒里我们认识了甲醇；从大米、瓜子里我们认识了石蜡；从田螺、火腿里我们认识了敌敌畏；从咸鸭蛋、辣椒酱里我们认识了苏丹红；从火锅里我们认识了福尔马林；从银耳、蜜枣里我们认识了硫磺；从木耳中认识了硫酸铜；从河鲜、海鲜中我们体会到了孔雀石绿；今天三鹿又让同胞知道了三聚氰胺的化学作用！”这段被MSN中文网白领用户票选为“2008年最值得珍藏的MSN签名”之一的调侃文字却似乎无奈地成为了当今中国食品安全现状的写照。<br />
赵连海在他的博客文章里，数次质问造假者的良心，质疑有关部门的不作为：“难道他们就不会再多一些公众责任心，多一些即便是目前有经济损失也要把没有检测过的产品都先下架的勇气吗？难道非要再多出很多“结石人”后在公众的指责下及有关部门的督促下才能极不情愿地将这些恐怖的食品下架吗？难道他们的钞票和股票就比公众的身体安危更重要吗？难道他们没想到自己的孩子长大后的伴侣有可能曾经是一个肾功能或者其它器官有所缺失甚至病变的患者吗？”<br />
然而至今为止，来自有关方面的回应使赵连海感到愤怒失望。<br />
从2009年开始，“结石宝宝之家”就遭完全屏蔽，在境内无法直接访问，只有使用国外代理服务器方能访问。其后，三十多个省的受害儿童家长QQ群被强行解散。<br />
赵连海在其博文《谨致：所有具有优良质量、善良博爱与美德的好人！》中无奈地写道：“太多家长的电话、网络等通讯被严密监视，太多家长被谈话“关心”，个别带头人被关押、跟踪及恐吓，与其他受害家庭的接触被强行拦截阻止。”“一些有正义、有良知的公益律师在义务地帮助我们这些已经饱经伤痛、势权全无的父母，但都被各地司法机关强行勒令停止对我们的所有帮助，有的律师被恐吓以至被彻底吊销了律师执照，太多想帮助受害家庭的好心人被谈话恐吓……”<br />
“结石宝宝之家”成立后，赵连海和其他维权人士一样，成了“重点关注对象”。他的家就几乎成了当地公安部门的新驻点，警察“光临”数十次，找他谈话问话，他的妻子和家人也多次被叫去秘密谈话，平日院子里也总有些行踪可疑的人在游荡。<br />
“我博客上的文章，只要稍微有一些敏感或者是留下我联系方式的，十分钟都不到就会立刻被删掉。”赵连海打开自己在某门户网站的博客页面，点了一根烟，摇了摇头，“这已经是第四个博客了，前三个都给和谐了。”<br />
<br />
四<br />
赵连海认为，他所做的不过是为自己和其它受害儿童家庭讨公道，一切合情合理，因此，他对有关方面的“特别关注”感到十分无奈。他说，官方可以不必对他过度紧张。“（做这个事）有几个想法，第一是为孩子们讨公道，孩子尚小他没有这个能力，我们做父母的自然是责无旁贷。第二是我们希望借着这个事件对国家的方方面面，司法也罢，制度也罢，有一个很好的推进促进。”。<br />
维权过程中受到的阻力使得赵连海感到无奈，而部分受害儿童家长态度的转变则让赵连海感到寒心。<br />
“结石宝宝之家”建立初期，每天的浏览量都达到四位数字，几千名家长先后以电话或者邮件的方式联系了赵连海，同他分享信息，QQ群上也是热闹非凡。然而2008年底，媒体透露国家赔偿方案后，一切都冷了下来。<br />
国家的赔偿方案规定“结石宝宝”死亡案例每例赔偿20万元，重症病例每例3万元，普通病例每例2000元。<br />
有的家长接受了赔偿方案，不再热心和赵连海联系，甚至从此销声匿迹。更多的家长虽然不满赔偿方案，但也不愿意再站出来和赵连海“并肩战斗”。“许多家长就这么冷眼旁观着，等着我们几个去抗议去维权。假如闹得有些成果了，他们也可以一起分享，假如没成果反倒被打压了，他们也不必冒险。”赵连海深深地吸了口烟，“这真的让我寒心。我总是这么对家长说的，为了孩子，我们还有什么可顾忌的？”<br />
但情况的转变却间接证明，很多家长有所顾忌。最初为提请诉讼，赵连海成功收集了几千名患儿的资料，但近几个月再次调查这些患儿目前的健康状况时，却仅仅回收了不到一百份问卷。巨大的落差，让赵连海经历了从沸点到冰点的转变。<br />
一位家长在数次挂掉赵连海打去的询问患儿近况的电话后，打了个电话回来质问，“你到底有完没完？”<br />
说到这，赵连海又点了一根烟，又重复了一遍：“为了孩子，我们还有什么可以顾忌的？”<br />
<br />
五<br />
赵连海说，尽管因为“毒牛奶”事件他对社会和政府抱有失望的情绪，但他不希望孩子将来对社会有抵抗情绪。对孩子解释他的病的时候，赵连海提到是“大坏蛋”在小朋友们喝的牛奶里下了毒，因此，在看到其它患儿治疗的图片时，赵连海的儿子几乎是咬牙切齿地发誓长大后要惩罚这些“大坏蛋”。<br />
当然，惩罚这些“大坏蛋”并不能完全解决问题。要妥善地解决问题，更重要的是保证“结石宝宝”们的将来得到保障。为此，赵连海设想成立一个“结石宝宝基金”，将募捐得来的资金用于医疗科研和帮扶三聚氰胺受害家庭。<br />
按照国家有关规定，发起公开募捐的机构必须在民政部正式注册，而注册的机构必须有至少50个自然人作为法人并提供至少10万的注册资金。资金的问题容易解决，让赵连海头痛的是法人从何而来？“这50个人哪里来？有没有坚定的50个人愿意站出来？因为毫无疑问，除我外的49个在当地肯定会受到高压。”<br />
即便能正式注册，家长们在“结石宝宝基金”的使用上分歧也不小。赵连海承认，许多家长对他提出的将基金大部分资金用于科研的提议嗤之以鼻，认为是非常幼稚且不可行的。<br />
除了可行性的问题，构想中的“结石宝宝基金”也同样存在着生存性的问题。如何能找到一个稳定且能干的团队便是摆在赵连海面前的一大难题。“从‘结石宝宝之家’成立至今，我们的工作团队已经换了好几批人了。原来一直是靠家长自愿承担一些工作，但许多人都不能坚持下来。家长们分配到的都是些很简单的工作，比如说管理论坛之类的，但慢慢地就不见人了。”<br />
没有一个稳定的团队是难以成事的，赵连海决定找艾未未（编者注：当代艺术家、社文评论员，诗人艾青之子）帮忙，组建一个团队进行调查，研究三聚氰胺是否对人体会产生长期的危害。“假如没有危害那就最好，我们就可以把关注的焦点放到赔偿问题上。假如有危害，那无论官方如何执意隐藏，我们都一定会为三聚氰胺的受害者们讨回公道。”</div>

							
							
							
							
