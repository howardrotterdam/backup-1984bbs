													<h2>三鹿破产案终结 结石患儿无法获得任何赔偿</h2>
						
						
																											<div id="postmessage_346088" class="t_msgfont"><a href="http://www.chinareviewnews.com" target="_blank">http://www.chinareviewnews.com</a>&nbsp; &nbsp;2009-11-28 16:33:23&nbsp;&nbsp;<br />
<br />
<br />
　　中评社北京11月28日电／备受关注的三鹿奶粉赔偿事件被画上句号。结石患儿将无法从三鹿获得任何赔偿。 <br />
<br />
　　法制晚报记者今天上午获悉，石家庄中院日前作出裁定，终结已无财产可支配的三鹿破产程序。裁定中显示，三鹿对普通债权的清偿率为零。这意味着，结石患儿将无法从三鹿获得任何赔偿。 <br />
<br />
　　昨天下午，首起结石患儿状告三鹿讨要赔偿的案件正式在法院开庭审理。 <br />
<br />
　　开庭伊始，作为主要赔偿对象的三鹿代理人向法庭出具了一份由石家庄中院作出的裁定书。其中表明，现三鹿破产财产最后分配已经完成，法院裁定终结三鹿普通破产程序，并于即日起生效，落款为11月20日。 <br />
<br />
　　该裁定书显示，三鹿对普通债权的清偿率为零。 <br />
<br />
　　按照法律规定，企业宣告破产后，成立破产清算小组对尚存的财产进行清算，而后，按照一定顺序进行偿还。 <br />
<br />
　　优先偿还的部分包括员工的工资和社保，此后是抵押债权，即如果有银行贷款，那么需要将房屋土地等不动产作为财产抵押给银行还债。最后，才涉及到偿还普通债务，包括对患儿的赔偿部分。 <br />
<br />
　　破产企业需要按照上述顺序还债，但一旦财产用尽，债务尚未还清，法院在查明后便会裁定破产程序终结，未还债务一般也就不了了之。除非企业再度恢复生产，获得偿还能力，还债程序有望再度启动。 <br />
<br />
　　据一直代理三鹿患儿索赔事项的彭剑律师介绍，三鹿在支付员工工资、社保以及将房产等抵押后，已经没有任何财产可支配。这意味着，在历时一年才获准立案索赔的结石患儿们，已经没有机会再从三鹿获得任何赔偿。 <br />
<br />
结石患儿索赔事件 <br />
<br />
　　2008年9月，三鹿奶粉被调查出受到三聚氰胺污染，致使婴幼儿患结石，部分患儿家属未接受赔偿方案。 <br />
<br />
　　2009年3月12日，三聚氰胺奶粉受害者法律援助团律师携带6名原告诉讼材料赴石家庄中院，立案庭法官告知“涉及三鹿奶粉受害者索赔的诉讼已经指定由石家庄市新华区人民法院管辖”。 <br />
<br />
　　2009年3月16日，新华区人民法院立案庭要求原告补交相关证据。此后，原告律师补交了证据。 <br />
<br />
　　2009年3月25日，石家庄市新华区人民法院受理全国首起“三鹿奶粉”事件民事赔偿案。<br />
