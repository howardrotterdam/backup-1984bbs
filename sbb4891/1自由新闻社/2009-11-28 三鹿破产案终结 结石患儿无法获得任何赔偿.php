<br />
2009年12月01日 16:47 来源： 深圳晚报 【字体：大 中 小】 网友评论(0) <br />
<br />
　　据《齐鲁晚报》11月29日报道，“记者28日上午获悉，石家庄中院日前作出裁定，终结已无财产可支配的三鹿破产程序。裁定中显示，三鹿对普通债权的清偿率为零。这意味着，结石患儿将无法从三鹿获得任何赔偿。”<br />
<br />
　　按照法律规定，企业宣告破产，财产清算之后按照一定顺序进行偿还。优先偿还的部分包括员工的工资和社保，此后是抵押债权，即如果有银行贷款，那么需要将房屋土地等不动产作为财产抵押给银行还债。最后，才涉及到偿还普通债务，包括对患儿的赔偿部分。<br />
<br />
　　很明显，“三鹿”患儿是不幸的，在他们人生的最初阶段就遭遇了这样的人生“不测”，而更不幸的还在于有可能连一分钱的赔偿都拿不到。由于报道中并无明确显示，因此我们无从知道究竟“三鹿”的钱是否足够支付员工和银行？还是在此之前就已经“断炊”，抑或者是“恰巧”在偿还完员工和银行之后，才成了 “对普通债权的清偿率为零”？<br />
<br />
　　因此，不得不问一句的是，“三鹿”的钱，都赔给了谁？为什么患儿们的获赔权，会被置于最后？<br />
<br />
　　三聚氰胺之害，曾经令人谈之色变，其对患儿们的恶劣影响也许将会持续终生。从2008年底起，就有有关民事赔偿的消息传出，有卫生部牵头的赔偿小组建立，还有22家企业的医疗赔偿基金。但是一年过去，现状却是“清偿率为零”，按照“惯例”，患儿们所能获得的赔偿也将是不了了之。<br />
<br />
　　不得不说，这又是令人失望的一条消息。也许，从法律上来讲，它并无漏洞，也无法追究，但是有关三聚氰胺的消息曾经令无数国人为之愤怒和感慨，也令众多企业都陷入其中无法自洁。说“三鹿”是罪魁祸首虽然有些过，但是“患儿”却是真实的存在。而今，如果不予以赔偿，也许在法律上并无过错，但人性和道德都会被钉上耻辱柱！<br />
<br />
　　由于兹事体大，从法律的层面，“三鹿”的钱都赔给了谁，有必要进行一番公示；而且案情特殊，在当初判决之时为何没有想到把对患儿们的赔偿置于首位？即便的确已经无力赔偿，而之前将大众胃口“钓”了一年的赔偿小组和医疗基金是不是也应出面给予合理的解释？中国的事情最怕拖，一拖之后，往往就是不了了之。然而于情于理，“三鹿”事件中最直接的受害群体也即那些患儿们，如果得不到赔偿 ，却实在是说不过去。<br />
<br />
　　“三鹿”的钱都赔给了谁？为什么患儿们不能突破一下法律的规定首先获得赔偿？曾经言之凿凿的声音如今都哪儿去了？政府不能食言，相关企业也不能缩起头来装作无事人；而那些患儿，应该更是在水深火热之中等待这赔偿来稍稍弥补今后可能伴随一生的病痛！<br />
<br />
　　至于如何赔偿，方法并不足虑，网友们给出的“股东利润赔偿”和“当地政府负连带责任”等都不妨一试。然而这一切还都是后话，现状最为急迫的，还是给“三鹿”已经赔出去的钱一个明确的说法。</div>

							
							
							
							
