访问的主要目的是增强互信,加强合作,夯实两国友好的基础。通过同鸠山首相的会谈,增进了两国领导人的互信,达成了一系列重要共识,其中包括重建中日总理热线、建立两国防务部门海上联络机制、商签海上搜救协定等。我们还就加强两国经贸合作进行了探讨,商定将绿色经济、能源环保、电子商务等作为重点领域。这些从根本上都有利于两国关系的发展。<br />
<br />
我以极大的热情进行了四场民间外交。第一场是在日中友好七团体和侨界四团体的欢迎晚宴上,我用“心”发表了一篇即席演讲。第二场是在日本经济界团体欢迎午餐会上,我用“理”发表了一篇即席演讲。第三场是同两国文化界人士座谈的时候,我用“情”同他们交流。第四场是在同日本民众和学生接触中,我用“行”与他们交流。通过这四场活动,我感觉到,中日两国民众的心灵和感情是可以沟通的,中日合作是有着广阔前景的。现在,中日两国经济紧密相联,谁也离不开谁。我们还要加强文化和人文交往,这是我们友谊的根,是我们友好的基础。<br />
<br />
现在看来,我访问的目的达到了,访问是成功的。<br />
<br />
国谷裕子:刚才听到您热情洋溢的话我深受感动,日中两国在2006年就决定建立战略互惠关系,这具体体现在哪些方面?关于东海油气田开发问题,这次双方同意正式启动落实东海问题原则共识的政府间换文谈判,我认为这是很好的事情,是双赢的。您对此有何想法?<br />
<br />
温家宝:中日就东海问题达成原则共识后,双方领导人对此事都很关心。我常讲,好事多磨,水到渠成。双方决定正式启动政府间换文谈判,这朝着解决东海问题的方向迈进了一步。我们主张本着搁置争议、共同开发的原则妥善解决东海问题,这最符合中日两国人民的利益。双方需要相互理解,形成共识,逐步推进,使东海真正成为和平之海、友好之海、合作之海。<br />
<br />
<br />
国谷裕子:当前全世界比较关注中国对“天安号”事件的表态。中国的想法到底是什么?我们看来,中国采取比较慎重的态度,同朝鲜和韩国都保持一定距离,为什么?是由于对事件的背景不了解,需要继续观察,还是不想刺激朝鲜?<br />
<br />
温家宝:“天安号”沉没是一起不幸的事件。对于韩国人民尤其是遇难者家属的悲痛心情,我们表示理解,并向他们表示慰问,多次对遇难者表示哀悼。<br />
<br />
中国是一个主持正义、负责任的国家,在处理这个问题上,我们没有任何私利。我们主要的着眼点就是维护朝鲜半岛和平稳定。我们重视韩方和其他国家进行的联合调查及各方的反应,将根据事情的本来面目,也就是事情的是非曲直来决定立场。<br />
<br />
我们呼吁各方冷静克制,不要使事态升级,更不要发生冲突。因为发生冲突,受害最深的是韩朝两国人民,中国也难于幸免。中国有一句成语:城门失火,殃及池鱼。这样的感受只有相邻的国家才会有。在这个问题上,我同李明博总统先后进行了约4个小时的深谈。我们的谈话一直是在友好、坦诚和相互理解的气氛下进行的。<br />
<br />
中国的立场是鲜明的:一是一贯主张并致力于维护半岛和平稳定;二是坚持通过六方会谈解决半岛的无核化问题。这两条都关乎东北亚和平和安定,六方会谈更具有根本意义,只有实现半岛无核化才能实现半岛长治久安。<br />
<br />
日本是东北亚重要国家,也是中国和韩国的近邻,我们希望在维护东北亚安全与合作等重大问题上同日本进行合作。昨天我同鸠山首相进行了小范围会谈,原定15分钟,实际谈了半个多小时。<br />
<br />
国谷裕子:韩国可能本周内提议联合国安理会讨论“天安号”事件,国际上对此也有期待,对中国的期待也很高,希望中国更积极地做朝鲜工作。中国对此持何态度?<br />
<br />
温家宝:中方理解李明博总统及韩国政府当前的困难处境,也理解韩国人民的心情。刚才我已经讲过,中国将依据事情的本来面目决定自己的立场。我们将认真了解、研究各方面的情况和反应,公正、客观地表明态度。我们在安理会上会怎么做,请主持人继续观察。<br />
<br />
国谷裕子:看来韩国处理起来很为难,但中国夹在朝韩之间,是否也很为难?<br />
<br />
温家宝:我多次表示,我们不袒护任何一方,我们会秉持公正的立场,但是我们对问题看得更为深远,也就是说任何处理方式都要从根本上有利于半岛和平稳定。<br />
<br />
<br />
国谷裕子:正如温总理所谈,东北亚稳定与和平是最为重要的事情。中国的军力快速增长,海军力量增强。另外,在中国国力增强的同时,中国年轻人爱国主义上涨,排外情绪上升,这令人担忧。您怎么看这个问题?<br />
<br />
温家宝:关于中国的军力,其实你想知道的是军费的增长。中国的军费在世界上是不高的。多年来,中国的军费占国内生产总值的比重不足1.5%,而世界平均水平是4%,况且中国的军费主要是用于人员开支,我不必过多解释这种情况。重要的是,中国的军事发展是透明的。中国在近现代史上饱受列强凌辱,我们深知和平、独立来之不易。中国奉行防御性的国防政策,唯一的目的是自卫,从来没有侵占过别国一寸土地。无论从哪方面看,中国对任何国家都不会构成威胁。我这次在日本的几次演讲中都强调,中国坚持走和平发展道路,永远不会对任何国家构成威胁,永远不称霸。<br />
<br />
至于谈到中国青年的爱国精神,我以为这是应该加以肯定的,我本人就是一个爱国主义者。我曾说过这样的话:我的每一个细胞、每一滴鲜血,都是爱国的。即使我死了,烧成灰烬,也是爱国的。关键问题是,一个人、一个民族,还要懂得平等地对待那些平等待我的国家和民族,为构建和谐世界做出自己的贡献。我们要教育年轻人,关心世界,关心整个人类的命运,也就是说要具有世界眼光。<br />
<br />
国谷裕子:日中政府之间的关系得到了很大改善,但是两国人民特别是年轻人之间的亲近感似乎不如过去,有待改善,为什么彼此之间有这样的隔阂?如何改善?最大的问题和课题是什么?<br />
<br />
温家宝:谈到中日两国人民之间的隔阂,归根到底是如何认识历史和现实的问题。我们之所以强调以史为鉴、面向未来,决不是延续仇恨,而是为了增进两国人民的感情和友谊,不让历史悲剧重演。如果谈到现实,二战以后,日本宪法规定日本要走和平发展道路。这给日本带来了实实在在的利益,日本经济快速发展,人民生活水平得到很大提高。新中国成立60年,特别是改革开放30年来,中国经济社会也发生了很大变化,我们提出要坚持和平发展、和谐发展。也就是说,中日都要走和平发展道路,两国人民应该相互理解、相互信任、相互合作。<br />
<br />
我重视增进两国民众的相互理解,我对两国民众之间的友好从来不悲观。从我自己的经历看,三年前我访问日本,在国会演讲时,赢得11次掌声,直到我离开国会大厅,走到很远的地方,还能听到掌声不断。在从东京到京都的路上,我看到日本国民自发地站在道路两旁,挥手欢迎。我同日本农民交朋友,同日本青年交流,同他们一起打棒球,同他们通信。我知道人与人之间的思想、感情和良知是可以沟通的。年长的人是这样,年轻人也是这样。对年轻人,要加以教育和引导。两国的领导人、新闻媒体、教育界都要做沟通两国人民情感的工作,促进相互理解。<br />
<br />
我提个建议供你们参考,希望媒体今后不要再公布两国国民尤其是青年相互不信任的所谓民调数字了,要把功夫下在沟通两国人民情感的实实在在的工作上。<br />
<br />
<br />
国谷裕子:您的建议非常好,我们今后更应该增强相互理解才行。我就中国国内政治经济提个问题。中国的贫富差距似乎在拉大。您提出要在余下的三年左右任期内,竭尽所能实现社会公平和正义,解决收入分配问题。您准备怎么做?<br />
<br />
温家宝:中国发展不平衡的一个主要问题就是城乡之间、地区之间存在贫富差距,个人收入之间也存在贫富差距。坦率地讲,这种差距还有拉大的现象。一个社会如果财富只集中在少数人手里,那么注定这个社会是不公平的,因而它也是不稳定的。<br />
<br />
在今后三年任期内,我们在应对国际金融危机、促进经济发展的同时,要更加注重促进社会公平。在这方面,我要尽最大努力。<br />
<br />
首先是教育,教育公平是最大的公平。我们已经实行了真正意义的9年免费义务教育,但这还不够,还要进一步发展高中阶段的教育,特别是职业教育。我们已经决定,对于职业教育要实行奖助学金制度。首先对于农村贫困家庭的子女和学农的学生要免学费。我们还实行大学生奖助学金制度。两年前,中国的高校入学率为20%,在校生超过2000万,但是当时奖助学金只有18亿元。我们用两年时间把它提高到350亿元,奖助学金的覆盖率达到40%。<br />
<br />
我们对农村给予特别的关注,因为农村的孩子生活条件相对较差,学习条件相对也较差。对农村的孩子,除了免除学杂费,还免除课本费;对于农村寄宿生,还要给予补助。<br />
<br />
教育公平就是为人人提供同等的受教育机会,我们将继续把这件事情做好。<br />
<br />
其次是医疗。医疗涉及每个人的健康,我们决定推进医药卫生体制改革。医药卫生体制改革在任何国家都是一件非常艰难的事情。为了推进医药卫生体制改革,中央政府将在最近3年内投入8500亿元人民币,做几项重要的工作。<br />
<br />
第一、在农村实行新型农村合作医疗制度,就是政府拿出120元,个人拿出30元,作为统筹基金,记在农民个人帐户上,实行大病统筹,按不同比例报销。农民有了这笔资金,就可以上乡卫生所、县医院乃至省会医院看病。随着政府财政的增长,我们将不断提高新农合政府补助的比例。<br />
<br />
第二、在城镇,我们实行居民基本医疗保险,主要是管“一老一小一残”。也就是说,主要是由国家出资,地方和个人配套,让这部分群众能够有钱看病。至于城镇职工,享有医疗保险待遇,退休后继续享受。<br />
<br />
第三、社会保障。社会保障主要指几项重要的保险:养老保险、失业保险、医疗保险。这是三项最重要的保险,另外还有工伤保险,我们现在正在逐步推广这些保险。我只给你举一个例子,就是农村也要实行基本养老保险制度。农民到了60岁,就可以拿到基础养老金每月55元,这在过去农民连想都不敢想。农村和城市低保已基本普及,覆盖面达到90%以上,城市超过2700万人,农村超过4000万人。<br />
<br />
此外,农民还免除了农业税,这是中国2000多年种粮纳税历史的终结。农民种粮不再交任何钱,国家反而要给予补贴。<br />
<br />
当然,我们实行的社会保障制度是低水平、保基本、广覆盖的。我们国家太大,人口太多,我们需要逐步提高水平。<br />
<br />
为了缓解收入不平衡的状况,我们将采取多种措施,在一次分配中提高职工收入的比重;在二次分配中,政府财政支出更多向弱势群体以及教育、医疗等公共事业倾斜。<br />
<br />
所有这些工作都已提上议事日程并在逐项落实。只有实现一个公平正义的社会,才能体现这个社会应有的价值,才能让人民得到幸福和尊严。<br />
<br />
<br />
国谷裕子:您如何看待中国的政治改革?中国将如何推进民主化进程?<br />
<br />
温家宝:我们的改革是全面的改革,包括经济体制改革、政治体制改革和社会管理体制等方面的改革。没有政治体制改革的成功,也不会有经济体制改革的成功,我们从一开始就是这样想的,也就是这样做的。<br />
<br />
政治体制改革概括起来应该注重解决四方面问题:第一是建设社会主义民主政治,保证公民的选举权、知情权、参与权、监督权;第二是完善社会主义法制,依法治国,建设法治国家;第三是实现社会的公平正义;第四是实现人的自由和全面发展。<br />
<br />
