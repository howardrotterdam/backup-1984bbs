													<h2>石首 “秋后算账”开始了</h2>
						
						
																											<div id="postmessage_172579" class="t_msgfont"><a href="http://gcontent.nddaily.com/6/d1/6d19c113404cee55/Blog/fde/c2916e.html" target="_blank">http://gcontent.nddaily.com/6/d1 ... log/fde/c2916e.html</a><br />
<img src="http://pics.nddaily.com/6/d1/6d19c113404cee55/Thumb/14334/dcga152210.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
死者家属出具的一纸“免责”协议书。<br />
&nbsp; &nbsp; 上月平息的石首事件余波未了。在过去的10余天里，死者涂远高的家属分批被当地警方带走接受调查，至今已有5人被刑拘。手持“免责”协议的家属不满“秋后算账”，昨日已授权律师声讨说法。<br />
<br />
&nbsp; &nbsp; 死者家属向记者出示的一纸协议显示，死者遗体6月25日火化前，当地政府曾与死者家属签署过一份协议。如今，对于其中的“免责”条款，双方有着不同的理解。<br />
<br />
&nbsp; &nbsp; 另据记者了解和石首电视台报道，“6・17”事件后，当地公安部门已抓捕数十名涉嫌打砸抢烧人员。7月30日之前，将从重从快判决第一批参与打砸抢烧者。<br />
<br />
&nbsp; &nbsp; “免责”协议曝光<br />
<br />
&nbsp; &nbsp; 这份《关于6・17事件与死者家属有关事项的协议书》，拟定日期为“2009年6月24日”，分别由“市善后协调领导小组负责人郭子信、张芸安”，“家属代表涂德强、涂茂海、涂远华”签字。<br />
<br />
&nbsp; &nbsp; 协议书称：“经市6・17事件善后协调小组与死者家属代表达成协议……死者家属在‘6・17’整个事件过程中所发生的非组织、参与打砸烧的其他行为(如拉横幅、买东西的行为)，市政法机关免予处理……若死者家属未参与永隆大酒店纵火，则不负担责任……死者家属在2009年6月25日5时前自行理丧火化尸体后，此协议生效。”<br />
<br />
&nbsp; &nbsp; 家属称，6月24日深夜及次日凌晨，仍有部分家属不同意签字火化死者尸体，当时政府谈判人员催促尽快火化，“不要把事情搞大，现在还有台阶下，搞大了，台阶都下不来”，并表示可不追究家属在事件中的刑事责任。家属终于在25日凌晨3点多签署了这份协议，4点死者尸体火化。<br />
<br />
&nbsp; &nbsp; “没想到，现在他们不认账了。”家属说。<br />
<br />
&nbsp; &nbsp; 5家属被刑拘<br />
<br />
&nbsp; &nbsp; 6月27日，死者涂远高的堂侄涂志军第一个被警方带走调查，4天后回家。7月8日，市镇领导在高基庙派出所开会，列出一个8人家属名单，要求当日投案自首。<br />
<br />
&nbsp; &nbsp; 据了解，名单中最开始包括死者伯父涂德强，后来镇领导中有人说他在事件中一直陪着领导，“是正面的”，抓他说不过去，就划掉了，改为其女涂远芳代替。名单中还有死者表哥李光绪，也因有人说他什么都没做，抓他太离谱，随后改为其弟李光富。家属认为，“这是定的指标，就是要抓8个人。”<br />
<br />
&nbsp; &nbsp; 名单送达后，家属自认没做什么，“没有罪为什么要投案自首？”都没有去。9日，石首市公安局一辆警车载来6名警察，手持传唤书和手铐，分别将名单上的人带走。“手铐并没有使用。”被带走的家属说。<br />
<br />
&nbsp; &nbsp; 被带走的8人分别是死者哥哥涂远华，堂哥涂茂海，堂姐涂晓玉、涂远芳、曾玉梅，姑妈涂么饵，舅妈周晓春，表哥李光富。48小时后5人被放回，涂远华、涂茂海、涂晓玉3人被刑拘，羁押在石首市看守所。<br />
<br />
&nbsp; &nbsp; 7月13日，又有死者另3名亲属，舅妈周晓春、堂侄涂行军、表兄周志伟被警方带走，后两人随后被刑事拘留。<br />
<br />
&nbsp; &nbsp; 由石首市公安局开具的刑事拘留通知书称，被拘者均“涉嫌聚众扰乱公共场所秩序、交通秩序罪”。<br />
<br />
&nbsp; &nbsp; 依口述写《悔过书》<br />
<br />
&nbsp; &nbsp; 家属还转述7月17日从石首市和高基庙镇司法所两位主要负责人处得知的消息，“6・17”事件后，除5名家属被拘外，还关了28名涉嫌参与打砸抢烧的群众，另有约30人在逃，而被带走调查的群众达100多人。<br />
<br />
&nbsp; &nbsp; 死者家属前后有17人被调查。其中一位家属在收到《传唤通知书》后被带到石首市公安局，48小时后回家。在公安局，她被要求交代在“6・17”事件的行为。她说，我什么都没做也没参与，只是以一个死者家属的身份出现在现场。“他们说我站在路中间就是堵路，阻塞了交通。我说过一句‘跳楼自杀怎么没有血’，就说我扰乱了群众的心，要我认罪。”<br />
<br />
&nbsp; &nbsp; 回家之前，这位家属被要求写《悔过书》和《保证书》。她不写，公安局一位徐姓人员说，我说你来写。她就按对方口述写下《悔过书》和《保证书》，大意为：在“6・17”事件中，我违反了公共场所秩序、交通秩序罪，保证在“6・17”案件处理中不外出，不串供，随叫随到……<br />
<br />
&nbsp; &nbsp; “免责”的不同理解<br />
<br />
&nbsp; &nbsp; 石首市政府办公室副主任郭子信，是在“免责”协议上签字的政府代表。7月8日，定下8人家属名单后，郭曾入户动员家属投案自首。家属问，你签的字，为什么不算数了？郭说，我们是不愿追究你们的责任，但法律会追究你们的责任。他还说协议是对“非组织、参与打砸烧的其他行为”不追究，但没说对“有组织”等行为不追究。另一在协议上签字的高基庙镇党委书记张芸安，事后希望放回被抓的家属，但却表示没这个权力。<br />
<br />
&nbsp; &nbsp; 昨日上午，赶到石首的北京市京都律师事务所孙广智律师，与家属签了授权书。孙律师告诉记者，“免责”协议是否具法律效力，现在还不好说。如果家属存在犯罪，法律一样会追究，而不会看是否有协议。而从协议的“免责”内容看，实际表述意思是如果没有犯罪，就不追究刑事责任，这跟没说一样。只是在签协议时，家属将对方的意思理解为，是对他们事后免予追究刑事责任。<br />
<br />
&nbsp; &nbsp; 京都律师事务所另一律师宣东，将于今日抵石首。孙律师表示，他只是宣东律师的助理，具体案情要等宣东律师看过材料后，再作商议。<br />
<br />
&nbsp; &nbsp; 石首事件该如何反思<br />
<br />
&nbsp; &nbsp; 7月12日，中共中央办公厅、国务院办公厅印发的《关于实行党政领导干部问责的暂行规定》正式公布。该规定明确，对滥用职权引发群体性事件，或者对群体性、突发性事件处置失当等七种情形，将对党政领导干部实行问责。<br />
<br />
&nbsp; &nbsp; 石首事件起因是一起普通的非正常死亡案。面对诸多疑问，当地政府现场处置没能及时以合适的方式解释沟通，以至引发大规模群体性冲突。据此，中央电视台、《人民日报》等中央媒体均用报道、评论等方式进行过质疑。但据记者了解，石首事件平息后，至今没有当地官员被问责。<br />
<br />
&nbsp; &nbsp; 一位石首受访者认为，“6・17”群体事件的内因，是当地群众对地方治安不满长期积郁的结果，地方官员应多从自身反省。如何处理善后，如何对待事件中个体的某些违法行为，也反映着政府对这一事件反思的深度和态度。<br />
