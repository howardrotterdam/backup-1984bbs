<br />
【大陆吹左风】随着2012年中共十八大的来临，上自中央，下至地方正展开新一轮的权斗，为十八大时的人事布局作准备。这由中共第三代领导人江泽民和第四代领导人胡锦涛在建政 60年庆典时的表现；重庆市委薄熙来大搞红色热潮打黑风暴，公然挑战被视为胡锦涛嫡系的广东省委书记汪洋，以及近期传出薄熙来後院起火，即他曾主政的辽宁省掀反腐风暴，被视为是江泽民人马的省纪委书记王唯众落马，胡江两派权斗可见端倪。北京资深传媒人李大同称，中共官场刮左风，最终都是为了一个「权」字。<br />
