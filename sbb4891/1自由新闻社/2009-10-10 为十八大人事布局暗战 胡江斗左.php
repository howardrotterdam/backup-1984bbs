对於胡锦涛近期大打左风，北京资深传媒人李大同对本报说，「（胡锦涛）治国七年没有任何建树，他只能打毛泽东的大旗，彰显自己是正统的权力中心。」另一名北京学者司马瀚认为，虽然现在距十八大还有近 3年时间，但现在是拉帮结派，打压政敌的时候了。<br />
