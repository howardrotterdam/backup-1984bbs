<strong>薄熙来重庆打黑　剃胡派汪洋眼眉</strong><br />
<br />
据悉，被喻为太子党的国家副主席习近平，之所以未能按惯例在上月底中央四中全会上出任军委副主席，与他是江系人马，前国家副主席曾庆红推举丶胡锦涛仍要观察不无关系。事实上，负责中央人事安排的中组部，近期就接班人问题不断发指令，要求各级在考察接班人事的工作上，务必增加「是否贯彻科学发展观」（即胡锦涛的观点），作为重要考核指标。<br />
<br />
当前，中南海权斗日炽，地方亦不遑相让。封疆大吏薄熙来（ 60岁）和汪洋（ 54岁）备受关注。 2007年，汪洋从重庆调往广东後，大搞「腾笼换鸟」丶「解放思想」等理论和政策，频频在媒体上曝光大出风头。但薄熙来更出位，接任重庆市委书记後，大搞全民唱红歌丶读红书丶讲红色故事，让重庆变成红色山城。<br />
<br />
更值得注意是，薄熙来今年 8月突然又打着「为民除害」的旗帜，在重庆大肆扫黑，数以百计当地高官商贾纷纷落马或入狱，其中不少是汪洋主政重庆时的爱将，被外界解读意欲打击政敌汪洋。深圳民间学者朱建国直指，薄已获北京左派人士的支持，左派网上出现一片赞扬他的声音，这对他在十八大上更上一层楼，将有着重要影响。<br />
