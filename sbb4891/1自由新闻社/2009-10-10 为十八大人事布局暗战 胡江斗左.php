被视为胡锦涛嫡系的汪洋， 2007年由重庆调广东省委书记後， 08年开始大搞「解放思想」丶「腾笼换鸟」等理论和政策，希望粤能在政治丶经济等领域走先全国一步，获媒体大事报道，大出风头<br />
<br />
07年接替汪洋，出掌重庆市委书记的太子党薄熙来（左）亦不甘示弱， 08年中开始亲自点燃「重庆文化大革命之火」，今年并大肆打黑，被指明彰正义，暗踩中共十八大的劲敌汪洋 <br />
<br />
