<strong>左网涌现「胡哥您好」</strong><br />
2009年10月10日 <br />
<br />
中共为保建政 60周年庆典不受干扰，全力打压不同声音，封杀异见网站，但对左派网站却网开一面，令左网站在十．一期间大行其道。庆典当日大阅兵巡游队伍出现「毛泽东思想方阵」时，这些左派网站不约而同齐声欢呼，有人更贴文称呼「胡哥您好！」<br />
<br />
<strong>议政网站被强令关闭</strong><br />
<br />
内地着名左派网站包括「乌有之乡」丶「毛泽东旗帜网」丶「巍巍昆仑」等。过去几年，它们成为内地下岗工人丶失地农民和城市贫苦大众诉苦阵地，也是自称继承毛泽东思想的毛派们集中地，曾多次遭当局下令关闭。<br />
<br />
今年十．一前，内地数以千计各类议政论政的网站被强令关闭，但惟独这些网站继续大行其道。<br />
<br />
十．一天安门阅兵巡游队伍出现毛泽东思想方阵，更令这些左网兴奋无比，网上出现大量赞颂文章和跟贴。有贴文称，「锦涛同志不愧是毛主席的好学生」丶「江泽民让我们下岗，胡哥一定要为我们伸w！」有网民甚至贴出「胡哥您好！」表示敬意。<br />
<br />
但也有网民称要小心防止有人「打着毛泽东的旗号，继续做祸国殃民的事情！」<br />
