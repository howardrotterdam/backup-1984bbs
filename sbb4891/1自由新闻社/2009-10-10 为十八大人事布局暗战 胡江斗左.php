<strong>习近平说「共产党八股文」</strong><br />
2009年10月10日 <br />
<br />
正在欧洲访问，被视为胡锦涛接班人的中共新任政治局常委丶太子党习近平，近来也常常表现出「左态」。《人民日报》昨刊发他以《加强和改进新形势下党的建设的纲领性文献》为题的文章，内容充斥左倾论调，被内地网民讥为「共产党式的八股文」。<br />
<br />
习近平未能在上月闭幕的中共十七届四中全会上当上军委副主席，令外界关注他接班是否受阻。在内地一片左风下，他近期的言论，也被指充斥共产党教化。<br />
<br />
主管港澳事务的习近平鲜有出位言词，但今年 2月 11日访问墨西哥与当地华侨见面，他突然爆肚指摘少数外国人对中国事务说三道四：「有些吃饱了没事干的外国人，对我们的事情指手画脚。中国一不输出革命，二不输出饥饿和贫困，三不去折腾你们，还有甚麽好说的！」<br />
