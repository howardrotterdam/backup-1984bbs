<strong>胡吁世界媒体增公信力</strong><br />
2009年10月10日 <br />
<br />
中共对传媒控制不遗馀力，令官方喉舌的公信力备受质疑。不过，这并不妨碍中国国家主席胡锦涛就媒体的职责高谈阔论。他昨日出席世界媒体o会时，一再强调，传媒要承担社会责任，要提高公信力。<br />
<br />
<strong>中国拘禁记者最多</strong><br />
<br />
世界媒体o会由新华社与新闻集团丶美联社丶路透社等全球八大媒体集团倡议发起；来自世界 70多个国家和地区 180家媒体和机构负责人共 300多人出席o会。胡锦涛在大会致辞中称，媒体应秉持高度社会责任感，不断提高公信力，客观报道之馀，更应广泛传播共赢包容理念。<br />
<br />
中共建政 60周年，全球聚焦中国，争相到北京采访，其间发生多宗境外和外国记者被粗暴对待甚至殴打事件，但o会出席者均不触及这些事件。胡锦涛在会上承诺，中方将按有关法规，加强保障外国记者在中国的合法权益。<br />
<br />
本次o会主题是「合作丶应对丶共赢丶发展」。而无国界记者组织早前公布「世界新闻自由指标」评比，中国的新闻自由度在全球接受评比的 173个国家中位居 167名，排倒数第六。中国也是全球拘禁记者最多的国家之一。<br />
