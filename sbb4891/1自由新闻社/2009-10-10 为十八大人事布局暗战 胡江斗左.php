<strong>中共传统　借左风打异己</strong><br />
2009年10月10日 <br />
<br />
中共史上借左风夺权丶打倒异己事件屡见不鲜。自 1921年 7月 1日中共在上海成立， 88年来党内权斗不息，尤其是毛泽东掌握中共话事权後，更喜欢以左（代表革命之意）的名义肃整党内，打倒异己；毛的继承人包括林彪丶「四人帮」等亦无不高举左派大旗，俨以正统自居，争权夺利。<br />
<br />
<strong>毛以「生活不检点」打高岗</strong><br />
<br />
毛泽东自中共创党後在党内地位一直沉浮，直至 1936年红军长征抵达陕北後，威望才逐渐建立。<br />
上世纪 40年代初，他在延安发起大整风，将党内对手或异见一一打倒，其中共产国际派代表王明的罪名就是「右倾保守」；他又提出「文艺要为工农兵服务」的左派口号，影响中国文艺半个多世纪。<br />
<br />
中共建政後，毛泽东更是左得发狂，用「生活不检点」为由，整倒要跟他分庭抗礼的党内对手高岗；借反右运动，打倒不同意搞大跃进的国防部长彭德怀；以文革的极左方式，打倒党内对手同时也是同乡的刘少奇；他在临死前，甚至留下一句话：我死後，右派一定会翻天！<br />
<br />
几十年来，毛泽东的继承人无不以左派自居。他钦点的接班人林彪带头在天安门城楼高呼「毛主席万岁万万岁！」以「谁反对毛主席就打倒谁」为藉口，将党内和军队内大批对手整倒；他的夫人江青，以及王洪文丶张春桥丶姚文元组成的四人帮，更以左派代表自居，文革後期与所谓代表右派的邓小平等中共元老争权夺利。<br />
