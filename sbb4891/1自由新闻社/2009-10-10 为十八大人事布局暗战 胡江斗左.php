<strong>话你知<br />
胡拒入「八一」大楼</strong><br />
「八一」大楼位於北京复兴路 7号，也称国防部大楼，紧邻中国人民军事博物馆。邓小平去世後，时任军委主席江泽民下令在上址兴建中央军委办公楼， 1997年 3月动工， 1999年 8月建成，主体建筑地下 2层地上 12层，据称有地下有车道与中南海和西郊军方西山指挥中心连通。大楼建筑总投资 7.79亿元人民币。<br />
<br />
2002年江泽民交出总书记职後，以中央军委主席身份搬入八一大楼办公。直至 2004年 9月胡锦涛就任中央军委主席为止。<br />
<br />
但胡锦涛接任军委主席後拒绝搬入八一大楼办公，令这座豪华又威武的中央军委办公楼失去价值。现在八一大楼主要用於中共军方外事活动。该大楼门前无任何标识，只有国徽及门边肃立的哨兵。<br />
