													<h2>马英九总统声援保钓</h2>
						
						
																											<div id="postmessage_836232" class="t_msgfont">来源：<a href="http://zgmy.ownbb.com/viewtopic.php?f=5&amp;t=144" target="_blank">http://zgmy.ownbb.com/viewtopic.php?f=5&amp;t=144</a><br />
<br />
&nbsp;&nbsp;　中评社台北9月18日电（记者　倪鸿祥）中日钓鱼台事件让钓鱼台群岛主权争议再起，外界关心马政府这次所持的立场，马英九和媒体餐叙时私下指出，钓鱼台是在甲午战争后，日本人才窃占，事实上历史也是这样，这次政府表态不只适当也正当，过去申张主权，也比日本、大陆都要早。 <br />
<br />
　　这次中日钓鱼台事件争议期间，台湾“外交部”也两度重申钓鱼台为固有领土，民间也有人发起保钓运动，9月14日还发生台湾渔船遭日方海上保安厅7艘巡视船干扰，“外交部”也向日方提出抗议。 <br />
<br />
　　马英九17日中午与媒体餐叙，当他逐桌和记者闲话家常时，告诉中评社记者，当年（1971年）发起保钓运动，学生发动示威，事后一位“外交部”父执辈的官员对他说，学生示威还是有用，这对于政府在跟日本人谈判的时候很有帮助，可见舆情蛮重要的，所以那一次的保钓运动还是有用的。 <br />
<br />
　　他说，实际上来讲，美国于1972年冲绳归还协议，认为日本人还有残余主权，但我们（“中华民国”）这边不承认，后来美国改正说只是交还行政权，主权的问题要你们自己（中日）去讨论，美国到现在都还是这个意思。<br />
<br />
保钓人士指美日安保条约已将钓鱼台列屿归为日本所有，马英九指出，美日安保条约认定很模糊，因为日本人说钓鱼台是琉球的一部分，但我们说不是，我们认为钓鱼台是在中日甲午战争后，日本人才窃占，事实上历史也是这样。 <br />
<br />
　　他表示，1894年11月以前（甲午战争），清廷在海上、陆上都战败，日本的内阁会议在1895年1月通过将钓鱼台划入日本版图，虽然是在4月17日签订马关条约之前，但实际上很清楚的是在清廷战败之后。 <br />
<br />
　　他指出，日本过了7年后（1902年）才公布，完全违反日本政府作业的方式（明治宪法中规定，法令必须经过天皇饬令通过才可以生效具有效力，但关于钓鱼台的事项，日本天皇并没有发布饬令，因此不具有法律效力）。 <br />
<br />
　　马英九说，日本政府在兼并琉球时1875年先发布敕令（日本勒令琉球停止对中国的朝贡关系），等于总统府公报，这都很清楚，“所以我们适当的表达绝对是正当的，而且比日本、大陆都要早”。 <br />
<br />
　马英九就读台湾大学法律系时，听完丘宏达演讲后，引发研究钓鱼台国际法问题的热诚；1970年美国宣布将琉球及钓鱼台列屿交给日本，引发美国的台湾留学生不满，他于1971年在台参与保钓运动；后来留学美国哈佛大学的博士论文《怒海油争：东海海床划界及外人投资之法律问题》，就是对钓鱼台问题的研究。 <br />
<br />
　　留学毕业返台后，马英九将该论文中的若干数据取出，结合联合国刚颁布的《海洋法公约》，再加入新近的研究与看法改写为《从新海洋法论钓鱼台列屿与东海划界问题》专著；2003年他担任中国国际法学会理事长，也曾对钓鱼台主权问题举行深入座谈，这位“保钓急先锋”、“热血青年”的称号，一点也不假。</div>

							
							
							
							
