<br />
6时15分许，温家宝来到汉江边，他身着一套带有北京奥运标志的运动服，绕着江边的一块草坪开始跑步。一些正在这里锻炼的首尔市民认出了温家宝，走过来向温家宝问好。<br />
<br />
有四位中年男子相约在这里跑步，看到迎面跑来的温家宝，他们放缓脚步，温家宝微笑着伸出手来与他们握手，并询问他们是做什么工作的。这四人告诉温家宝，他们一个是教师，两个是从事建筑业的，还有一个在咖啡馆工作。<br />
<br />
　　“你们认识我吗？”温家宝问。那位教师说：“经常在报纸上看到您的照片，昨天在电视新闻里也看到您了。”<br />
<br />
同四人道别后，温家宝继续绕着草坪跑步。跑了两圈后，他看到草坪上有人在打羽毛球，便走上前去。两名正在打球的青年男子认出了温家宝，迎过来向他问好。其中一人将自己的羽毛球拍让给温家宝，温家宝与另外一人进行了一场“友谊赛”。<br />
<br />
　　打了几个回合，温家宝兴致更浓，索性脱下外套，双方的比赛也更加激烈。打了约五、六分钟后，温家宝示意比赛停止，“你打得比我好，”他笑着说。比赛结束后，两位新结识的“球友”聊起了家常，告别时，温家宝说：“代我向你的家人问好。”<br />
　　这时，有一对夫妻走过来，丈夫叫高昌燮，妻子叫李长顺。看到温家宝，李长顺显得颇为激动：“温总理，我是您的‘粉丝’。”温家宝笑着说：“我对韩国人民也有深厚的感情，我这次来访问，也是来看望大家的。”<br />
<br />
　　“我们都很喜欢您，希望我们两国能建立一种‘幸福的关系’，也希望您每天都身体健康，工作顺利。”李长顺说。<br />
李长顺问温家宝喜不喜欢韩国的食物，温家宝连说喜欢。“我昨天还吃了一个韩国的小火锅，还有泡菜。”<br />
<br />
　　不觉之中，太阳已经从汉江边升起，工作人员提醒温家宝该回去了。温家宝意犹未尽，看到旁边有几个小伙子在打棒球，他又走了过去。2007年访问日本时，温家宝曾与日本大学生切磋棒球，此后便得了一个“35号投手”的雅号。今天，他在韩国民众面前展示自己的棒球技艺，也赢得了一片喝彩。<br />
<br />
　　温家宝在汉江公园的晨练活动持续了约半个小时，离开前，他再次向锻炼的首尔市民挥手致意，感谢同他们一起度过了一个愉快的早晨。<br />
