<br />
<br />
另外你也算和中共打交道多年，难道连中共的宣传习惯也不了解？如果这次死的人真的主要是中共镇压维人的结果，他们是绝不会可能主动报道140人死亡的（这一数字我隔夜听到也大吃了一惊），除非外界已经开始披露事实真相而且实际死亡人数要高得多。。。那么反过来说，中共之所以这么迅速、这么爽快地报道死伤人数（去年拉萨骚乱后对相关数字还遮掩了半天），就说明它判断这对自己有利无害，换句话说，那些死伤者大多数确实是出自维族报复汉人的结果-----之所以现在官方传媒尚没有开始大肆声讨，我想是为了留出时间让全国尤其全疆各地的汉人、官员、建设兵团等先做好应付更大骚乱的准备，在确信不怕维族人继续闹事或者汉族人反击报复（极不可能但抗议可能会有）之后再慢慢利用传媒和网络（这次可能官媒反而会保持克制，以免激发逆反心理）释放对维族人不利的事实真相。<br />
<br />
<br />
即使以上的都不论，也很难想象这里的民运准民运对安魂曲“偏听偏信”如此看不惯，却对另一种“偏听偏信”的吾尔开希不置一言-----我之所以问这里的讨论是否可以公开，就是希望大家想想民运究竟有没有勇气面对汉族占了绝大多数的国人？------躲在这样的小圈子里批判安魂曲容易，可你们敢公开发表一份同情维族人却不谴责砍杀平民的声明么？你们有吾尔开希那样勇敢面对自己民族的勇气么？<br />
<br />
<br />
安<br />
<br />
<br />
<br />
第一，我说的当然不一定是绝对事实，而是基于我个人理性和经验的判断；<br />
<br />
<br />
第二，我想我和你得出的结论是基本一样----那就是中共的民族政策失败（包括对少数民族过多照顾偏袒）才造成了今天的局面，而且中共有责任向全世界公布韶关和乌鲁木齐两起民族冲突事件的真相或至少是关键性的死伤具体情况，并且应该允许独立的海外传媒进行不受干涉的采访报道。<br />
<br />
<br />
第三，但我和你有所不同的是：我认为这起事件的主要责任者除了专制中共之外，也明显应该包括伊斯兰极端势力-----伊斯兰极端势力已经成为全人类的第一大严重威胁，我们应该至少和反对中共一样旗帜鲜明地反对它，否则就可能被其所用贻害无穷。<br />
<br />
<br />
第四，具体到民运来说，无论如何也不应该在这个问题上给世人留下偏听偏信疆独组织的印象-----我之所以这么说是有依据的，在我发言前，蔡楚等就主动传播疆独组织的单方面说法，这和单方面宣扬中共新闻联播说法没有任何性质差别。而在汉人大量被杀、国内汉族对维族人长期以来敢怒不敢言的情况下，民运这么做就等于主动离间自己和普通民众的关系。。。可笑可耻的是：某些民运只敢在这里批判安魂曲、或者利用转载转贴什么的偏帮疆独，却不敢公开发表声明阐明自己对此事的观点，因为他们不敢把自己的内心暴露给国人。<br />
<br />
<br />
安魂曲<br />
<br />
<br />
<br />
我想问先生一句：<br />
<br />
<br />
您觉得很有必要把韶关某个工厂的维汉斗殴事件渲染成什么“韶关种族仇杀案”么？您在广泛传播这种观点时想过其社会后果没有？您此刻是否认识到对韶关斗殴案件民族冲突性之的过分夸大渲染，实乃这次乌鲁木齐暴乱的直接起因？<br />
<br />
<br />
难道下次某个学校、单位出现了某地学生群殴其他地方学生的事件，您也要定义其是什么“地域仇杀”么？上次法国巴黎、澳洲海滩都发生穆斯林和当地人斗殴事件，同样造成人员死伤，您觉得谁会吃饱了撑的代表本种族忏悔或道歉？<br />
<br />
<br />
安<br />
<br />
<br />
<br />
我的本能反应和吾尔开希的本能反应其实没什么不同------他是维族人，所以出了事先声称死的人一定多是维族，而且是军警大肆镇压的结果；我是汉族人，我听说死的大多数是汉族平民，而且很多是在小巷子里被杀的，感情自然会让我站在汉人一边。<br />
<br />
<br />
本民族同胞死于无辜，正常的反应大抵都该如此，所以我不怪吾尔开希（如他所说：我们的“父老乡亲”并不同)。。。这件事情其实和疆独中共专制什么的根本都没有关系，也就是说：不能因为你反对疆独，因此见了这种骚乱就一定大骂维族人；也不能因为你一贯反共，明明死了大量汉族人也非要高喊维族人有理。<br />
<br />
<br />
当然本能反应可能有偏见、有差错，这个就要慢慢了解事实真相了。。。。我之所以在这里发言，不是要大家都认同我此时此刻的看法（维族杀害无辜汉人），而是提醒民运：不要为了“政治正确（其实也毫不正确，因为连基本人权人道都不讲）”就急吼吼把自己绑上热比娅的战车，那样绝对是政治自杀。<br />
<br />
<br />
安<br />
<br />
<br />
<br />
早在八十年代我就和维族人同过学，所以我说维族人平时比汉族人抱团得多，而且打起架来十分勇狠，因此汉族人根本不敢惹他们。。。这绝对是有切身体会的。我自己事实上和维族人混得不错，从来没受过他们欺负（维族人一般也不象小痞子那样乱惹人），但我的同学因为维族人某种奇怪的逻辑（比如他让你喝酒你不肯喝、欠了钱晚还、打群架时自己没动手。。。等）就被暴打的事情却屡见不鲜。<br />
<br />
<br />
作为一个汉族人，我知道自己民族绝对不如维族人能抱团、敢斗狠。<br />
<br />
<br />
安<br />
<br />
<br />
<br />
“大肆屠杀无辜汉族同胞”的事情我认为确实发生了（这个时候需要我们每个人自由心证），事实上早几天我就有预感，觉得维族不会对韶关斗殴事件如此善罢甘休-----和上大学时别的学校有维族人受气，全城维族学生都会去帮打架的道理差不多，只可惜我们汉族人从没这么团结过。<br />
<br />
<br />
当然我并不强求别人相信这一点（如果那样的话，我一定会专门写文分析论证）。<br />
<br />
<br />
和单个维族人交往，我并不觉得你们有任何不妥，但作为一个整体我确实觉得你们比我们汉族人团结得多也敢斗得多。。。这其实是一个民族相对更优秀的标志，但站在我的民族立场来看，我就不得不在疆独等问题上多考虑那些已经住在新疆了的汉人今后前途等问题----新疆如果真的成了东土耳其斯坦国，那里的汉族人口再多也一定会很快沦为弱势民族：他们连中共都不敢反抗，到时候就更不敢得罪维族人了。。。如果你吾尔开希当头，我还是放心的，因为你有这么多汉族朋友而且居然难得的不一边倒支持疆独，但换<br />
<br />
了别人呢？<br />
<br />
<br />
而且如果新疆实行民主制的话，人口更多的汉族光靠投票就可以避免维族人上台，这个问题你们考虑清楚了没有？如果没有，那就只好或者专制、或者驱逐汉人。<br />
<br />
<br />
安<br />
<br />
<br />
<br />
不要故意偷换概念：问责政府是一回事情，偏袒冲突一方则是另一回事情----真要问责政府，就应该呼吁政府立即公开死伤人数详情和事件全过程，而不是在网上如你一般传递所谓“韶关事件真相”，故意给大家造成维族人有理汉族人不对的印象。<br />
<br />
<br />
安<br />
<br />
<br />
<br />
如果你对迪里夏提“让维族人到广东做工是强迫奴隶劳动”的鬼话居然“轻信”的话，我只能说你满脑子只懂得反共，除此之外已经失去基本的判断力。<br />
<br />
<br />
安<br />
<br />
<br />
<br />
这里有两件不同的事情----谴责中共治理民族问题无方和反对大陆新闻封锁、要求放开国际传媒进疆，让世人了解真相是绝对必要的，也是民运针对此事声明的重点。<br />
<br />
<br />
但另一方面中国民运也必须要旗帜鲜明地谴责针对无辜民众的故意（！）杀害，反对一些疆独组织将韶关事件等普通治安斗殴事件上升到民族对立高度的一些宣传。。。民运如不方便可以对此保持沉默，但却绝不能够反过来帮助宣传维人的一面之词，或者把事件美化成什么“维族抗暴斗争”。<br />
<br />
<br />
你说“维族人在没有组织的情况下，突然杀害那么多无辜生命的可能性实在太低”，这个在逻辑上并不成立-----事实上恰恰因为维族人没有什么组织控制又有强烈的报复韶关事件心态，才很容易演变成针对汉人的民族仇杀。无论如何乌鲁木齐已经有100多人死亡，谁也不相信那居然会是汉人平民围攻维族人的结果，因此唯一剩下的可能就是军警对维族人展开的屠杀，所以搞清楚死者的民族比例在眼下最为关键，相信无辜平民大量死亡的真相很快就会通过网络等形式传播出来，到时候希望大家都能自圆其说。<br />
<br />
<br />
安魂曲<br />
<br />
<br />
<br />
再重复一句：<br />
<br />
<br />
说维族人屠杀无辜汉人，并不等于就不该谴责政府民族政策失败、不该要求大陆公布全部事件真相尤其死亡人员详情、开放独立的国际传媒进疆报道。<br />
<br />
<br />
不要故意把这两件事情搞混----难道问责政府就非得预设立场，基本同意维吾尔人大会的一面之词么？<br />
<br />
<br />
安<br />
<br />
<br />
<br />
而且民运不可能也不应该完全脱离自身的民族特点----在发生民族、种族冲突的时候，中国民运和未来的任何中国政府当然要首先关心自己血浓于水同胞的生命财产安全。<br />
<br />
<br />
人家维吾尔“民运”、藏人“民运”就从没有忘记这一点。当年孙中山的革命能够成功，主要也是靠了排满的口号-----如果当年有人在海外抗议汉族革命党滥杀满人什么的，他早被历史淘汰了。<br />
<br />
<br />
今日中共固然以汉族为主，这点和当年满清不同。。。但中共内部也有大量维族官僚（按人口比例更高）-----新疆自治区的主席和大量军警都是维吾尔人，凭什么反共非要弄得支回反汉似的？<br />
<br />
<br />
安<br />
<br />
<br />
<br />
大家看看下面这条新闻，为了激起各民族的反共情绪，是否也需要和韶关械斗案一样大肆渲染这是一起苗汉之间的“种族冲突”呢？<br />
<br />
<br />
安<br />
<br />
<br />
明报／贵州继上月底发生百馀村民围堵黄果树风景名胜区事件后，前天在贵定县再发生群众抗议事件。当地一条高速公路项目因取土问题，引发数百名村民不满，双方发生n突，造成11人受伤。直至昨日上午，事态基本平息，当地政府正在进一步调查事件。<br />
<br />
据新华社报道，涉事的工程是贵州省夏蓉高速公路第6标段，工程公司项目部因为取土填地基，引发布依族苗族自治州贵定县昌明镇秀河村村民不满。5日下午，村民与施工人员争执并出现n突，双方各有200多人，造成11人受伤，部分财物受损。伤者被送往医院救治。事件发生后，贵定县委书记及县长立即赶往现场处置，并成立了处置该次「群体性事件指挥部」，至当晚12时左右，现场群众已疏散。<br />
<br />
有贵州网民称，当时两班人马数百人在昌明镇华东路桥对峙：「个个手持铜棍，场面好吓人，公安迟迟不到现场控制场面，只有派出所的几个警察。我看了一会就赶快跑了。」</div>

							
							
							
							
