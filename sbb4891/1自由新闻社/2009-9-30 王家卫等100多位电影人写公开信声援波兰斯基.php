　　波兰斯基昨日提出释放申请<br />
<br />
　　上周日，为了在瑞士的苏黎世电影节上领取终身成就奖，曾执导《钢琴师》、《苦月亮》的名导罗曼・波兰斯基在入境瑞士时因为31年前的通缉令被当地警方收押，正等待洛杉矶方面启动引渡程序。据悉，如果他被引渡至美国，很可能将被判处终身监禁。<br />
<br />
　　本周一，波兰斯基的代表律师埃尔夫・特米姆称波兰斯基情绪稳定，将为自由“奋战到底”，“他目前斗志高昂，决定为自己辩护。”特米姆已从巴黎飞往苏黎世为波兰斯基争取保释，不让他被引渡至美国。波兰斯基还聘请了一位瑞士律师洛伦兹・埃尔尼处理此案。他们已于昨日向瑞士警方提出释放申请。<br />
<br />
　　现年76岁的波兰斯基，1977年因为与13岁女模特萨曼莎・盖莫发生性关系而被控以强奸等六项罪名。1978年，经过审讯，法院判其猥亵未成年人一项罪名成立。波兰斯基在临审前夜从美国逃亡到法国。尽管当年猥亵案的受害人萨曼莎・盖莫已表示早已宽恕波兰斯基，但波兰斯基再也不能回到美国。在过去31年流亡岁月中，波兰斯基一直避免入境与美国有引渡条款的国家（比如英国），但他经常造访瑞士，甚至在瑞士境内的滑雪胜地哥斯塔德拥有一幢别墅，经常与他的演员妻子艾曼纽尔・塞尼耶以及两个孩子一起在此度假。<br />
<br />
　　欧洲各国给予外交支持<br />
<br />
　　法国外交部长贝尔纳・库什内称这次31年后的拘捕行动“用心险恶”。法国文化部长弗雷德里克・密特朗表示，看到波兰斯基“因为原本就没什么道理可言的陈年旧事被送入虎口”，他非常担心，并表示法国总统萨科齐对这一案件非常关注，将密切跟踪案件的进展。萨科齐办公室尚未就此事发表声明，不过他的内阁发言人勒费夫尔表示，得知洛杉矶检方居然等了这么多年并以如此“古怪”的方式做出拘捕行动，他感到很震惊。勒费夫尔的副手帕伊补充说，看到一个向来标榜“民主”的国家，居然无视时效法，这非常令人震惊，“不承认时效法的民主是一种非常古怪的民主。”法国前文化部长杰克・朗称这次拘捕行动“不可思议”而且“不合理”。他补充说：“在欧洲，根据时效法，20年前的指控都不能成立。”<br />
<br />
　　上周刚刚在美国的支持下当选联合国教科文组织主席的保加利亚驻法国大使伊琳娜・伯科娃此次也站在了反对美国的立场上，她称“看到一位举世闻名的知识分子被以如此奇怪的方式拘捕”十分令人遗憾。<br />
<br />
　　拥有法国和波兰双重国籍的波兰斯基也得到了波兰政府的外交支持。波兰外交部长拉德克・斯科尔斯还联合法国外交部长库什内向美国国务卿希拉里发出专函，希望她能干预波兰斯基的引渡程序，允许波兰斯基保释，并希望得到美国总统奥巴马的特赦命令。<br />
<br />
　　<strong>世界电影人声援波兰斯基<br />
<br />
　　包括中国香港导演王家卫在内的超过100位电影人也公开声援波兰斯基。王家卫与希腊裔流亡导演科斯塔・加夫拉斯、意大利女星莫妮卡・贝鲁奇、西班牙导演阿莫多瓦、德国导演文德斯、法国影星范妮・阿当等，在戛纳电影节组委会牵头下签署了一封联名信，称波兰斯基被捕事件是“不可接受”的，并称波兰斯基是“当代世界最伟大的导演之一”，“法国的、欧洲的、美国的、全世界的电影人均对这一事件表示关切。”</strong><br />
<br />
　　包括著名导演安杰伊・瓦伊达在内的波兰电影人也大力呼吁瑞士释放波兰斯基。波兰电影人协会已经要求美国检方重新考虑引渡决定。在德国，柏林电影节主办方也要求瑞士当局当即释放波兰斯基，德国电影学院也强烈谴责瑞士对波兰斯基的拘禁行为。在美国，独立电影大厂温斯坦兄弟影业老板哈维・温斯坦也积极为波兰斯基请愿。“我们联系了所有可能帮得上忙的电影人，希望能帮助他渡过难关。”温斯坦说。<br />
<br />
　　波兰斯基最新电影《枪手》已经杀青，但后期工作尚未完成，该片原定于明年2月的上映计划被迫搁置。该片改编自一部同名小说，小说作者罗伯特・哈里斯对此表示关切。“肯定发生了什么。”哈里斯说，“他经常去瑞士，要是想逮捕他，瑞士警方有那么多机会，为什么偏偏选择在他去领奖时？”<br />
<br />
