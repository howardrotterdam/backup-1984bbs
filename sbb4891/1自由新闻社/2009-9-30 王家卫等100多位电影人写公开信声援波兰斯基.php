																											<div id="postmessage_264760" class="t_msgfont">[编辑本段]3、作家林达夫妇<br />
　　林达，是一对美籍华人作家夫妇合用的笔名。另有“丁林”、“Dinglin2”等笔（网）名。夫为丁鸿富，妻为李晓琳。他们都于1952年出生在上海，1978年进入大学。曾在黑龙江省插队。1991年移居美国。<br />
　　林达的书，在中国大陆风靡一时，被誉为是介绍美国最好的作者之一，是中国的托克维尔。同时，林达在《南方都市报》、《新京报》、《南方周末》等报刊开设专栏，不仅有时事评论，还有散文和特稿。林达的文字朴实自然，富有洞见，写作领域涉及宪政、法治、历史、国际关系，是少有的全能型作家。<br />
[编辑本段]著作<br />
　　“近距离美国”系列： <br />
　　《历史深处的忧虑》（之一）（台湾版名《辛普森案的启示》） <br />
　　《总统是靠不住的》（之二） <br />
　　《我也有一个梦想》（之三） <br />
　　《如彗星划过夜空》（之四） <br />
　　旅游笔记 <br />
　　《带一本书去巴黎》<br />
　　《西班牙旅游笔记》（台湾版名为《西班牙像一本书》） <br />
　　文集 <br />
　　《扫起落叶好过冬》<br />
　　《一路走来一路读》 <br />
　　《像自由一样美丽――犹太人集中营遗存的儿童画作》 <br />
　　译作<br />
　　《汉娜的手提箱》<br />
　　《克拉拉的战争》 <br />
　　《盖比橱柜的秘密》<br />
[编辑本段]背景<br />
　　从1997年以来，林达先后出版了“近距离看美国”系列《历史深处的忧虑》、《总统是靠不住的》、《我也有一个梦想》以及《从边缘看世界》等书，行销数十万册，引起很大反响，今年出版的《带一本书去巴黎》也成为热门读物。 <br />
　　其实林达是两位作者合用的笔名。在上个世纪90年代初移居美国后，就像他们在一本书的前言中所写：最初落脚点的选择十分偶然，他们落在了美国南方，那是在现代机械化大农业挤兑之下的传统农村。 <br />
　　他们因同样的谋生劳作开始和美国的普通人建立友谊，开始了解这个不寻常的国家。他们看到的美国完全在意料之外，法治所保障的，是个人不受到他人或政府的侵犯。在这个保障下，百姓五花八门地享受着最大可能的个人自由。个人自由，这才是美国公民最关注和维护的东西。这一切让人好奇。 <br />
　　于是林达开始给朋友写信，不仅写出见闻，也试图写出美国现象的制度根源，写出大厦如何在常识常情的基础上建立起来，而每个普通人的个人自由，为什么竟可以是一个大国的立国之本。 <br />
　　这两年国内学术界正在反思法国大革命，反思卢梭，林达离开美国之后的写作涉及了巴黎，用林达自己的话说，其实只是触动了法国的一小段历史，可是这段历史曾赋予“革命”一词以神圣的光环，不论它裹挟着怎样的血腥，它似乎总是对的。正是由于另一种生活方式让人对个人自由有了新的观照，人才能够回过头来，重新打量法国式的自由口号，林达想写出民众曾经呈现的另一种面貌。</div>

							
							
							
							
