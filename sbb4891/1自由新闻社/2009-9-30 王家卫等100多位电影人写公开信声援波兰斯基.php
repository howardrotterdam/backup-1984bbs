<br />
　　追捕波兰斯基已多年<br />
<br />
　　被问及是否会将波兰斯基引渡到美国，瑞士当局表示，要看美国方面的决定。之所以过去波兰斯基入境瑞士都没有被捕，是因为瑞士当局没有事先收到通知，而这次美国方面获悉他将出席苏黎世电影节，因此向瑞士当局提供了情报。“我们得到的是正当合法的逮捕令申请，我们也知道他什么时候会来。”瑞士司法部发言人告诉媒体。<br />
<br />
　　美国检方驳斥了当年波兰斯基逃离美国后检方不作为的说法，并表示1978年以来他们就此事与英国、泰国、法国等多国警方均有接触。美国法警署的首席检察官托马斯・赫辛说，多年来，波兰斯基一直是国际刑警组织“红色通缉令”上的人物。<br />
<br />
