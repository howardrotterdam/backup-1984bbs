<br />
　　全会提出，党的基层组织是党全部工作和战斗力的基础，是落实党的路线方针政策和各项工作任务的战斗堡垒。必须坚持围绕中心、服务大局、拓宽领域、强化功能，进一步巩固和加强党的基层组织，着力扩大覆盖面、增强生机活力，使党的基层组织充分发挥推动发展、服务群众、凝聚人心、促进和谐的作用，使广大党员牢记宗旨、心系群众。要推进基层党组织工作创新，增强党员队伍生机活力，建设高素质基层党组织带头人队伍，构建城乡统筹的基层党建新格局。 <br />
<br />
