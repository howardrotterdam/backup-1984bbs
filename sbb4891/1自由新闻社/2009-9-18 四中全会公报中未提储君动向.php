<br />
China’s Xi Unexpectedly Not Named to Party Military Commission<br />
Share | Email | Print | A A A<br />
<br />
By Bloomberg News<br />
<br />
Sept. 19 (Bloomberg) -- China’s Vice President Xi Jinping wasn’t named a vice chairman of the ruling Communist Party’s Central Military Commission, signaling President Hu Jintao may want to hold on to some power after his scheduled retirement in 2013.<br />
<br />
Xi, 56, was expected by many China watchers to be appointed a vice chairman of the commission, which controls China’s 2.3 million-strong armed forces, at a meeting, or plenum, of the party’s Central Committee in Beijing which ended yesterday without any such announcement. His ascension to the post was seen as helping cement his status as the heir apparent to Hu. Ten years ago, Hu, then vice president, was named a commission vice chairman at a similar party meeting.<br />
<br />
Xi may still be named to the post in coming days or weeks or at a party meeting next year. Should he fail to be appointed, it may be a signal that Hu wants to hold on to his role as the commission’s chairman beyond 2013, delaying Xi’s full assumption of power, said Victor Shih, a professor at Northwestern University in Evanston, Illinois who studies elite Chinese politics.<br />
<br />
“If Xi indeed did not gain ascension into the CMC, it is indeed surprising,” Shih said. “My take is that Hu wants to delay Xi’s ascension into the CMC so that he himself can serve another full term as chairman of the CMC before fully retiring at the 19th Party Congress” in 2017.<br />
<br />
“For now, Xi accepts his exclusion from the CMC,” Shih said.<br />
<br />
Xi’s Consolation<br />
<br />
Xi may have been given a consolation for getting passed over, Shih said. He gave the explanation of a new document on Communist Party construction to the nearly 200 members of the Central Committee. The wide-ranging document emphasizes that lower-level Communist Party officials will be given more freedom to express their views in a move to expand so-called intra-party democracy.<br />
<br />
“The plenum gave Xi a lot of face,” Shih said.<br />
<br />
Xi’s failure to secure the position this year may signal that the military is not yet comfortable with him or it may reflect a recognition that the Communist Party has other important issues to settle, such as cracking down on corruption and weathering the global economic slowdown, before personnel issues should be addressed, said Russell Leigh Moses, a Beijing- based political analyst.<br />
<br />
“It is quite conceivable that he realizes that his own political prospects are improved by continuing to defer his ascension for another year,” Moses said yesterday before the meeting’s conclusion.<br />
<br />
China’s Princeling<br />
<br />
Xi, a former party boss in eastern China’s Zhejiang province and in Shanghai, was put in charge of coordinating the 2008 Summer Olympic Games in Beijing and is also responsible for the upcoming Oct. 1 celebrations marking 60 years of Communist Party rule.<br />
<br />
Xi is known by China watchers as a “princeling” because he’s the son of a prominent official. His father, Xi Zhongxun, who died in 2002, was a governor of southern China’s Guangdong Province and a vice premier. <br />
