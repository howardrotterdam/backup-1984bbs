<br />
　　全会对当前和今后一个时期加强和改进党的建设作出了部署，强调要建设马克思主义学习型政党、提高全党思想政治水平，坚持和健全民主集中制、积极发展党内民主，深化干部人事制度改革、建设善于推动科学发展和促进社会和谐的高素质干部队伍，做好抓基层打基础工作、夯实党执政的组织基础，弘扬党的优良作风、保持党同人民群众的血肉联系，加快推进惩治和预防腐败体系建设、深入开展反腐败斗争。 <br />
<br />
　　全会提出，世界在变化，形势在发展，中国特色社会主义实践在深入，不断学习、善于学习，努力掌握和运用一切科学的新思想、新知识、新经验，是党始终走在时代前列引领中国发展进步的决定性因素。必须按照科学理论武装、具有世界眼光、善于把握规律、富有创新精神的要求，把建设马克思主义学习型政党作为重大而紧迫的战略任务抓紧抓好。要推进马克思主义中国化、时代化、大众化，用中国特色社会主义理论体系武装全党，开展社会主义核心价值体系学习教育，建设学习型党组织。 <br />
<br />
