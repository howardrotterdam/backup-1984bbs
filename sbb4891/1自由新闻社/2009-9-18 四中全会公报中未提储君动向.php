<br />
　中国共产党第十七届中央委员会第四次全体会议公报<br />
<br />
　　(2009年9月18日中国共产党第十七届中央委员会第四次全体会议通过) <br />
<br />
　　中国共产党第十七届中央委员会第四次全体会议，于2009年9月15日至18日在北京举行。 <br />
<br />
　　出席这次全会的有，中央委员194人，候补中央委员163人。中央纪律检查委员会委员和有关方面负责同志列席了会议。党的十七大代表中部分基层党务工作者、基层党员和从事党建工作研究的专家学者也列席了会议。&nbsp;&nbsp;<br />
 <br />
<br />
　　全会由中央政治局主持。中央委员会总书记Hu Jintao作了重要讲话。 <br />
<br />
　　全会听取和讨论了Hu Jintao受中央政治局委托作的工作报告，审议通过了《中共中央关于加强和改进新形势下党的建设若干重大问题的决定》。Xi Jinping就《决定(讨论稿)》向全会作了说明。 <br />
<br />
　　全会充分肯定党的十七届三中全会以来中央政治局的工作。一致认为，中央政治局全面贯彻党的十七大和十七届一中、二中、三中全会精神，高举中国特色社会主义伟大旗帜，以邓小平理论和“三个代表”重要思想为指导，深入贯彻落实科学发展观，隆重纪念党的十一届三中全会召开30周年，团结带领全党全国各族人民大力弘扬伟大抗震救灾精神、北京奥运精神、载人航天精神，万众一心，迎难而上，锐意改革，共克时艰，全力保增长、保民生、保稳定，加大转变经济发展方式和调整经济结构力度，取得应对国际金融危机冲击、保持经济平稳较快发展的明显成效，推动深入学习实践科学发展观活动健康开展，加快推进地震灾区灾后恢复重建，维护民族地区社会大局稳定，全面推进社会主义经济建设、政治建设、文化建设、社会建设以及生态文明建设，全面推进党的建设新的伟大工程，各项工作取得新进展。 <br />
<br />
　　全会研究了加强和改进新形势下党的建设若干重大问题，认为在新中国成立60周年之际，进一步研究和部署以改革创新精神推进党的建设新的伟大工程，对于全面贯彻党的十七大精神，深入贯彻落实科学发展观，有效应对国际金融危机冲击、保持经济平稳较快发展，夺取全面建设小康社会新胜利、开创中国特色社会主义事业新局面，具有重大而深远的意义。 <br />
<br />
　　全会指出，中国共产党成立88年、执政60年、领导改革开放30年来，几代中国共产党人始终以实现中华民族伟大复兴为己任，坚持把马克思主义基本原理同中国具体实际相结合，团结带领全国各族人民不懈奋斗，战胜各种艰难险阻，不断取得革命、建设、改革的伟大胜利。我国相继实现了从半殖民地半封建社会到民族独立、人民当家作主新社会的历史性转变，从新民主主义革命到社会主义革命和建设的历史性转变，从高度集中的计划经济体制到充满活力的社会主义市场经济体制、从封闭半封闭到全方位开放的历史性转变，中华民族巍然屹立于世界民族之林。这是中国共产党人认识世界、改造世界的伟大创举，是根本改变中华民族命运、深刻影响人类历史进程的伟大变革。实践证明，没有中国共产党就没有新中国，就没有中国特色社会主义。办好中国的事情，关键在党。坚持中国特色社会主义道路，推进社会主义现代化，实现中华民族伟大复兴，必须毫不动摇地坚持中国共产党的领导。 <br />
<br />
　　全会指出，当今世界正处在大发展大变革大调整时期。世界多极化、经济全球化深入发展，科技进步日新月异，国际金融危机影响深远，世界经济格局发生新变化，国际力量对比出现新态势，全球思想文化交流交融交锋呈现新特点，综合国力竞争和各种力量较量更趋激烈，给我国发展带来新的机遇和挑战。我国经济建设、政治建设、文化建设、社会建设以及生态文明建设全面推进，工业化、信息化、城镇化、市场化、国际化深入发展，我国正处在进一步发展的重要战略机遇期，在新的历史起点上向前迈进。在我们这个十几亿人口的发展中大国，党在推进改革开放和社会主义现代化建设中肩负任务的艰巨性、复杂性、繁重性世所罕见。党要适应这样的新形势，统筹国内国际两个大局，更好带领全国各族人民聚精会神搞建设、一心一意谋发展，实现党的十七大描绘的宏伟蓝图，必须进一步加强和改进自身建设。 <br />
<br />
　　全会指出，当前，党的领导水平和执政水平、党的建设状况、党员队伍素质总体上同党肩负的历史使命是适应的。同时，党内也存在不少不适应新形势新任务要求、不符合党的性质和宗旨的问题。这些问题严重削弱党的创造力、凝聚力、战斗力，严重损害党同人民群众的血肉联系，严重影响党的执政地位巩固和执政使命实现，必须引起全党警醒，抓紧加以解决。 <br />
<br />
　　全会指出，世情、国情、党情的深刻变化对党的建设提出了新的要求，党面临的执政考验、改革开放考验、市场经济考验、外部环境考验是长期的、复杂的、严峻的，落实党要管党、从严治党的任务比过去任何时候都更为繁重和紧迫。全党必须居安思危，增强忧患意识，常怀忧党之心，恪尽兴党之责，勇于变革、勇于创新，永不僵化、永不停滞，继续推进党的建设新的伟大工程，确保党在世界形势深刻变化的历史进程中始终走在时代前列，在应对国内外各种风险和考验的历史进程中始终成为全国人民的主心骨，在发展中国特色社会主义的历史进程中始终成为坚强的领导核心。<br />
<br />
　全会认为，我们党在长期执政实践中，探索形成了我们党作为马克思主义执政党加强自身建设的基本经验，这就是：坚持把思想理论建设放在首位，提高全党马克思主义水平；坚持把推进党的建设伟大工程同推进党领导的伟大事业紧密结合起来，保证党始终成为社会主义事业的坚强领导核心；坚持以执政能力建设和先进性建设为主线，保证党始终走在时代前列；坚持立党为公、执政为民，保持党同人民群众的血肉联系；坚持改革创新，增强党的生机活力；坚持党要管党、从严治党，提高管党治党水平。这些基本经验，必须倍加重视、倍加珍惜，必须作为加强和改进新形势下党的建设的重要指导原则长期坚持，并在实践中不断丰富发展。&nbsp;&nbsp;<br />
 <br />
<br />
