　　朝鲜边境警备队属于人民武力部，但主要负责防止居民越境叛逃，管制非法出入境，因此迄今只配备步枪等小型武器。<br />
<br />
　　他表示：“82mm迫击炮与60L迫击炮不同，需要利用车辆进行运载。这次给每个小队配备四门迫击炮，意味着边境警备队具备了相当于步兵部队的火力。”<br />
<br />
　　此外，朝鲜在朝中边境地区还部署了朝鲜式远端火箭炮，朝中之间因此产生微妙的紧张气氛。<br />
<br />
　　一位消息人士表示：“朝鲜在咸镜北道巫山、两江道甲山等朝中边境地区周边，部署了122mm远端火箭炮旅团。”<br />
<br />
　　据悉，虽然122mm远端火箭炮的口径比240mm火箭炮较小，但其发射管数却多一倍，多达40个，实际威力不亚于240mm火箭炮。<br />
<br />
　　据悉，情报当局也掌握了朝鲜的上述动向，在调查朝方的意图。</div>

							
							
							
							
