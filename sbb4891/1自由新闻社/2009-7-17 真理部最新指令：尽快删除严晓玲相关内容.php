													<h2>严晓玲案中案 数百网友捞人 法院慌收场</h2>
						
						
																											<div id="postmessage_531907" class="t_msgfont">今天福建网民范燕琼、游精佑和吴华英遭当局“诬告陷害案”开庭，但开庭不到一分钟，法院公诉人即以补充证据为由申请延期，对此辩护律师刘晓原提出严重抗议，数百网友也在法庭外大声抗议和声援律师。(网友提供)<br />
<br />
严晓玲案中案 数百网友捞人 法院慌收场<br />
<br />
<br />
<br />
<br />
因关注严晓玲惨遭轮奸致死一案，福建网民范燕琼、游精佑和吴华英遭到当局以所谓“诬告陷害罪”逮捕。该案原本今天上午九点在福州市马尾区人民法院开庭。但开庭不到一分钟，法院公诉人即以补充证据为由申请延期，对此辩护律师刘晓原提出严重抗议，数百网友也在法庭外大声抗议和声援律师。 <br />
<br />
采访了在现场正和当局交涉的律师刘晓原，他说：“今天开庭，福建司法机关动用了上百的警察和便衣，拉了这么多警戒线，围观的群众也非常得多，但是开庭我们进去不到一分钟，法院就宣布休庭。检察官提出来，他们上次补充侦察一个月，没有找到诬告犯罪严重后果的证据，因此再次要求补充侦察，延期审理。” <br />
<br />
对于当局的草率作法，“我认为这种做法是利用法律程序，玩弄司法花招，特别是我，我从北京过来，法院当时打电话通知我来开庭，告诉我是当庭宣判，如果是当庭宣判的话，应该是前几天就搞好了，那么检察院提出来补充侦察，检察院和法院事前就已经商量好了，今天是走过场，他不可能法院准备宣判了，检察院突然提出来要延期审理。按照刑事诉讼法的规定，如果检察院没有足够的证据，法院就应该以证据不足为由，判决三个网民无罪的。”<br />
<br />
<br />
(数百网友庭外高喊刘晓原加油) <br />
<br />
数百网友庭外高声抗议 大喊刘晓原加油 <br />
<br />
为了声援三网民，来自全国各地网友组成了“网友关注团”前往福州声援。他们制作了横幅“公正正义，比太阳还要光辉”及抗议标语。 <br />
<br />
今天早上，马尾法院庭内上演了一场司法闹剧，庭外警察拉起了警戒线，并实行交通管制，上百个警察在法院外戒备。 <br />
<br />
记者在采访过程中，当三网友被警车载走的时候，听到现场传来阵阵高呼声：“我们支持你们，范燕琼多保重，游精佑你是好人，吴花英坚持住，我们都爱你……” <br />
<br />
从北京赶去福建声援的维权人士王荔蕻表示，有四、五百人去声援，警察非常无礼，非常粗暴，还打人，不知是警察和还是流氓，都分不清楚，跟他们要警证，他们也不给。游精佑侄子的相机被抢了，人也被打了。现在跟他们要相机，他们说不知道。 <br />
<br />
她说：“进去以后不到一分钟就出来了，说要继续补充侦察，刘律师非常愤怒，进去投诉他们，结果被警察推出来了，林洪楠律师根本不允许进法庭。范燕琼是从警车上抬下来的，都没让她女儿见一面，还来不及看到她妈妈，一分钟时间就完了，纯粹耍流氓。今天马尾有太阳，但是没有公平和正义。” <br />
<br />
有网友拍照及录像，遭到警察和便衣的殴打，并要抢走网友的照相机，但现场网友群体反抗，将抢相机的便衣打跑。严晓玲和吴华英母亲的东西也被便衣人员抢走。网友打100报警也无人理。 <br />
<br />
采访过程中，刘晓原正到法院去投诉，但法院不让他进去，还推他出去。现场网友立即高喊：“让律师进去，让律师进去。法院有没有司法，马尾有没有司法，无耻流氓竟敢推律师，有没有法律。刘晓原加油……加油……刘晓原加油，刘晓原加油……刘晓原我们支持你，我们支持你，刘晓原好样的。不要推律师，不要对律师耍流氓，耍流氓。马尾有没有法律……” <br />
<br />
现场也听到刘晓原正跟法院交涉：“这样叫我过来，你想开庭就开庭，你想延期就延期，这样做，这样一个普普通通的案件，它又耍了一个花招，又要补充侦察。补充侦察一个月。这是第二次开庭，也是第二次要求补充侦察。检察院这样做是浪费很多的资源、人力、物力。今天开庭又动用了这么多警察，浪费国家纳税人的钱。这是他们事先安排好的，故意耍花招……” <br />
<br />
刘晓原表示，如果你们要继续补充侦察，法院完全可以驳回检察院的要求，为什么还大老远把他们从北京叫过来，这就属于司法流氓的手段。<br />
<br />
<a href="http://www.youmaker.com/" target="_blank">http://www.youmaker.com/</a><br />
刘晓原正跟法院交涉，他据理力争 <br />
<br />
今天范艳琼的女儿林静怡未能见到日思夜想的母亲。她说：“我进去后不到一分钟就被拽出来了，没有看到妈妈，我妈妈没有出庭。上次我妈妈是吸着氧气、坐着轮椅出庭，现在我非常担心妈妈的身体。后来发生肢体冲突，我跪在地上不想出来，我要求见我妈，六、七个人把我拖到外面去，把我的手抓红了。林秀英所有材料被抢。” <br />
<br />
王荔蕻被警察围攻中，手机突然丢失。她表示，范燕琼两腿都瘫痪了，还不允许取保候审。游精佑把每个月的工资存起来，专门救助弱者，他是个高级工程师，是个非常好的人、有良知的人。吴华英的弟弟因为福清爆炸案，关押八年了，超期关押不放，她不断上访，就因为这个事情把她关起来。 <br />
<br />
此案件创造了两个全国第一 <br />
<br />
该案于去年11月11日首次开庭审理。审理后超过了一般的法定羁押期限仍无法下判，律师就此质疑超期羁押。但法院称，经过批准，本案须经马尾区人民检察院补充侦查。 <br />
<br />
刘晓原曾指出，这起案件可能创造了两个全国第一。因一篇“曝光”文章，以涉嫌诽谤犯罪为由而刑事拘留五个网民，这可能是全国第一次；将诽谤案件列为与国家秘密相关的案件侦办，这可能也是全国第一次。 <br />
<br />
