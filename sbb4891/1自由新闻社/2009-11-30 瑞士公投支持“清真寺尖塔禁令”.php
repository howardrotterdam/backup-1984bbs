政府以行政权力限制一家报纸的发行和流通，是不可以被接受的；因为只有专制国家，才由政府决定和左右媒体。在民主国家，由于不同程度的社会主义倾向，导致公立学校和政府资助的各种项目。但政府并不可以用出资为由，就来干预学校或项目内容的筛选。否则政府的行政力量就会左右大众思维，最后走向专制。<br />
在美国早就发生过这种争议，但解决的方向，却是朝向保护言论和新闻自由。一九九九年，纽约市布鲁克林艺术馆举行名为「耸动」（Sensation）的展览，其中一幅作品是：圣女玛丽亚身上被涂上大象粪。当时的纽约市长朱利安尼在展前批评说，如展出这种作品，市府将撤销每年给这家艺术馆的七百万美元资助，「你不能拿政府的钱，来亵渎宗教」。<br />
但朱利安尼的「威胁」，遭到美国媒体的强烈批评，认为是干预艺术表达自由。虽然该艺术馆是市府资助的，但政府却不可以因此干预具体的展览内容。如这种干预被允许，那么同样由政府资助的市立图书馆、博物馆等，进什么书，办什么展览，邀请谁演讲，是不是市府官员都有权审查和决定？最后纽约市政府的威胁起到了完全相反的作用，那件作品不仅得到展出，而且由于这一反面广告而观众倍增，政府也没敢停止拨款。<br />
虽然那件展品完全是极左派的垃圾，但对言论自由的保护，是不可对言论本身的内容有所筛选的，否则就不叫言论自由。也就是说，不是「好」的言论就被保护，而「坏」的言论就被封杀或限制。这个「好」和「坏」由谁来决定？最没有决定权的是政府。<br />
台北市府以「为孩子们好」的所谓「善」的名义，禁止对一家报纸的订阅。如果这开了先例，政府就可以用同样的名义，明天禁止一个电视节目，后天限制一家电台或杂志。在美国，那些专门刊登猎奇、煽情、耸动内容的小报（Tabloid），像《国家询问报》、《星报》等，每周发行一千万份，政府从未下令禁止中小学订阅。但这不等于学校就会都去订。<br />
孩子们应看什么读物，不是政府选择，而是由各学校董事会或教师等专业人员决定。如果学校「允许订阅」，而家长反对，那么家长可以控告学校，或让孩子转学。同样，如校方「拒订」，学生家长认为错误，也同样可告学校，到法庭为孩子争阅读权，最后由法官裁决。总之，孩子的道德教育和成长等问题，不可由政府决定。<br />
有人强调，有六成民调支持台北市府决定，因为这是保护孩子。但这种涉及言论和新闻自由的争议，不同于选举，它不属于靠民调、大众解决的范畴。而是靠专业、独立的司法，靠对言论自由的根基有深入理解的各级法官来裁决。总之，政府对言论的内容没有裁决的权力，更没有限制的权力。台湾曾长期在独裁统治下，很多人对政府限制言论自由警惕不够。但不要忘记，人类最沉重的灾难，都是在高举「好」和「善」的大旗下进行的。<br />
郝龙斌当选台北市长后，对他的国民党军头父亲说，「报告总长，山头攻下来了！」现在他又攻下了一个「山头」。如此攻下去，不用等到马英九跟对岸终极统一，台湾就会回到郝柏村的「戒严」时代了。&nbsp;&nbsp;<br />
（作者曹长青为独立评论员，<a href="http://caochangqing.com" target="_blank">http://caochangqing.com</a>）</div>

							
							
							
							
