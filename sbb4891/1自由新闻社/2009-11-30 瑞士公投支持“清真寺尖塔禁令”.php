瑞士公投支持“清真寺尖塔禁令”<br />
<br />
官方统计数字显示，在星期天（11月29日）举行的全民公投中，57%的瑞士人投票支持在该国禁止伊斯兰风格的尖塔的议案。<br />
<br />
　　提出这一全民投票建议的极右翼政党瑞士人民党说，清真寺尖塔是“伊斯兰化”的象征。<br />
<br />
　　领导反尖塔运动的人民党议员说，这样的尖塔象征着渴望获得建立伊斯兰法律和社会秩序的权力，而这违反了瑞士宪章的自由权内容。<br />
<br />
　　公投之后，瑞士一名资深穆斯林代表人物说，现在，瑞士的穆斯林担心，他们可能会受到更多的攻击，权利也会受到更多限制。<br />
<br />
<img src="http://i892.photobucket.com/albums/ac121/wodetuchuang/091129095320_swissminarets_226bafp.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
瑞士只有四座带有尖塔的清真寺<br />
<br />
　　禁止清真寺尖塔的议案需要在瑞士大多数省份得到通过，才能成为法律。<br />
<br />
　　BC在瑞士的记者说，公投的结果令人吃惊，对瑞士政府来说，也不是一个好消息。政府在全民公投举行之前，强烈呼吁该国公民投票否决人民党的这一提议。<br />
<br />
　　他们担心禁令如果通过的话，将在瑞士的穆斯林社区引起骚乱，还会破坏瑞士和伊斯兰国家的关系。<br />
<br />
宗教自由<br />
<br />
　　但是，记者还说，瑞士人担心移民数量大量增加和伊斯兰影响的扩大，因此对政府的呼吁置之不理。<br />
<br />
　　人民党为公投印制的一张宣传海报上，画着一个戴着伊斯兰面纱的妇女，背景是一面瑞士国旗，国旗上有好几个象征导弹的清真寺尖塔。<br />
<br />
　　海报上还用法文写着：支持禁止伊斯兰风格的尖塔！<br />
<br />
