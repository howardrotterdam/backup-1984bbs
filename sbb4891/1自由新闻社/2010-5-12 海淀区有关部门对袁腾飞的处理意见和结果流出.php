													<h2>袁腾飞因“错误言论”被处分</h2>
						
						
																											<div id="postmessage_614729" class="t_msgfont">资料图：袁腾飞<br />
被网友称为“最牛历史老师”的北京海淀教师进修学校高级教师袁腾飞正和出版商磨铁图书因为版税纠纷对簿公堂，同时袁腾飞的一些“语录”也在网上突然流传，袁腾飞更因为这些广为流传的“出格语录”被很多网友追捧，同时也被不少网友“围剿”。根据北京海淀区群众事务呼叫中心网站上的一份投诉记录显示，袁腾飞因为讲课时有“严重的错误言论”，已被责令检查、警诫谈话、批评教育。袁腾飞认为自己被出版商“恶意”陷害，出版商回复称这是“泼脏水”。昨天这份处理意见被网友发现并广为流传。至于网上传言袁被拘留，袁的律师表示应该不属实。<br />
责令作出深刻检查<br />
5月9日，一封编号为“Q2010050900026”的投诉信件出现在了北京海淀区群众事务呼叫中心的网站上，投诉称，曾上过百家讲坛的北京海淀教师进修学校高级教师袁腾飞的言论在网上风传，其被学生誉为“史上最牛历史老师”。袁腾飞看历史“是唯心主义的、更是反动的、有害的。是否定深入人心的社会主义历史。满腹经纶说的却是未经考证的野史。”“有关部门、上级单位是否对此人缺乏应有的监管?党组织是否缺乏对此人的党性教育?学历史的目的是知文化、以历谏今，我不明白他如此否定中国共产党、否定中国教育制度、否定中国政府对学生了解中国文化、发展我国社会有什么好处?”“他的行为是否涉嫌‘煽动分裂国家罪’?作为一个党员，是否没有起码的党性?希望有关部门重视起来。尽快给予社会一个满意的答复。”<br />
随后海淀区教委对该投诉进行了答复。海淀区教委称5月4日晚9点进修学校领导就接到了区教委领导关于此情况的通报电话。5月5日，学校相关领导对袁腾飞进行了“警诫谈话，批评教育”，并且“责令其作出深刻检查，深刻认识自己在网络视频中言论的严重错误，并要求其今后停止一切在民办培训机构等其他部门的授课活动”。“要以积极正确的态度对待此事，要配合有关方面做好工作，力争将影响降到最低限度。”<br />
“此事已经上报海淀区教育委员会，并通过区教委上报北京市教育委员会和相关部门领导。目前正在对袁腾飞本人进行教育批评过程中，对整个事件进行调查处理过程中。”<br />
海淀区群众事务呼叫中心的人员向南方都市报记者证实，该中心是海淀区政府的官方机构，而这只是已处理的一宗投诉，还有其他对袁腾飞的投诉尚未处理。<br />
另外，争论从网上延伸到了现实。5月9日下午，还有十多位网友到袁腾飞代课的精华学校找他“理论”。不过他们没找到袁本人，只是叫学校带话，“他必须给全国人民公开道歉，并到相关法律单位认罪!”<br />
被人恶意陷害?<br />
根据群众事务呼叫中心网站上的投诉回复，袁腾飞在接受学校调查时说，引起风波的是前几年在精华民办教育机构讲课时的录像视频，讲课中“出现不符合实事求是的错误言论”。原因则是“本人陷入与某出版商的解约官司，疑有人恶意找出，并上传至视频网站散播，意图是搅乱其与外界的关系。”<br />
5月6日，袁腾飞在人人网上发布了《关于“&lt;玩意3&gt;为非法出版物”的郑重声明》，指责宁夏人民出版社和磨铁图书的《历史是个什么玩意儿3》严重侵权，将通过法律手段解决，因此陷入了与磨铁图书的一场关于178万版税的网上口水战和网下官司，据悉，5月24日，双方将进行证据交换。袁腾飞所指“与某出版商的解约官司”正是此事。<br />
磨铁图书总裁沈浩波接受南都记者采访时否认了袁腾飞的说法，沈浩波称昨日在召开此事的新闻发布会后还有媒体记者欲让他加入“倒袁”阵营，但被其婉拒，“我不关心袁老师的个人言论，我跟他是商业纠纷，我不会因为这个去干涉别人的个人自由，这是我做人的底线，我也理解袁老师现在压力很大，但也不要给别人泼脏水，也应该有个底线。”<br />
袁腾飞本人的电话已处于停机状态，是否磨铁方面举报，他的代理律师余明旭向南都记者表示：“有这种可能，但目前尚无证据。”<br />
网上也在流传袁腾飞已被刑拘的消息。对此袁腾飞的律师余明旭告诉南都记者，他并不知道袁腾飞被抓的事情，“应不属实”。而南都记者致电海淀教师进修学校询问袁腾飞后续处理情况，校长办公室一位石姓老师称所有领导均在外开会，无法回应。<br />
袁腾飞简介：<br />
袁腾飞是北京海淀教师进修学校高级教师、精华学校教师，被学生誉为“史上最牛历史老师”。袁腾飞是高中历史新课标教材（人教版）编写者之一；曾参与北京市高考历史命题工作。于2009年7月13日-8月11日应邀登上央视《百家讲坛》节目讲授《两宋风云》。出版图书《历史是个什么玩意》、《两宋风云》、《塞北三朝》。先后被中国教育电视台《教育人生》和中央电视台《小崔说事》应邀，录制访谈节目。<br />
