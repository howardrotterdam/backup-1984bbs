袁腾飞是北京海淀教师进修学校高级教师。最近有关他的言论在网上风传，被学生誉为“史上最牛历史老师”。<br />
虽然公民有言论的自由，但是他的言论很多都已经违反我国宪法基本原则。他已经不再是否定一个“毛泽东”，而是在否定社会主义制度、否定中国共产党的领导、否定深入人心的社会主义历史。袁腾飞看历史是唯心主义的、更是反动的、有害的。满腹经纶说的确实未经考证的野史。<br />
