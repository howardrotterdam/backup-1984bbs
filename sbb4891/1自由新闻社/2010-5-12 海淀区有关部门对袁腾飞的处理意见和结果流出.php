来信人您好!您反映的信件， 区教育委员会进行了调查，并答复如下：5月4日晚9点进修学校领导接到区教委领导关于袁腾飞在网络视频上有错误言论情况通报的电话。近期，在网络上出现的袁腾飞的授课视频中，对毛泽东同志的评价有严重错误，造成非常恶劣的影响。 <br />
5月5日上午8点，学校校长陈宝民，总支书记、常务副校长罗滨与中学教研部主任张英等同志，集体与我校中学历史教研室教研员袁腾飞老师，就近期在某些网络视频中出现的袁腾飞讲课内容中严重的错误言论，进行警诫谈话，批评教育，并对此段视频发生的前因后果进行了询问和调查。 <br />
据袁腾飞本人阐述，上述视频内容，是在前几年精华民办教育机构讲课时的录像视频，由于当时对政治“时髦”话题的追捧，在讲课中出现不符合实事求是的错误言论。近期，因其本人陷入与某出版社的解约官司，疑被有人恶意找出，并上传至视频网站散播，意图是搅乱其与外界的关系。 <br />
袁腾飞2007年调入我校工作以后，就其在社会教育机构或其它外请授课的活动，学校一直持不支持的态度。2009年底，因袁腾飞在讲坛对中国历史 “太平天国”的评价讲课内容的问题，以及讲课及出书的问题，校长、书记亲自找其谈话、做工作，要求其正确对待份内工作和工作之外的事情，处理好各方面关系。 <br />
