讲到我国实现现代化目标的时候，却说“我们什么时候才能摆脱社会主义初级阶段，进入发达社会主义呢？我不恭维各位，你烂了也看不见。”<br />
袁腾飞现象的出现，究竟是商业炒作、是个人炒作、或真的如网友指出的是一个具有代表性的社会现象，值得深思。但是有关部门、上级单位是否对此人缺乏应有的监管？党组织是否缺乏对此人的党性教育？学历史的目的是知文化、以历谏今，我不明白他如此否定中国共产党、否定中国教育制度、否定中国政府对学生了解中国文化、发展我国社会有什么好处。<br />
