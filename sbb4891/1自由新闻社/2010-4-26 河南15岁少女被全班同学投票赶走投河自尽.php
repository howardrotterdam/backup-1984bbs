																											<div id="postmessage_584471" class="t_msgfont">河南15岁少女被全班同学投票赶走投河自尽<br />
2010-04-26 14:35:00　来源: 广西新闻网　跟贴 314 条 手机看新闻 <br />
<br />
核心提示：日前河南一名15岁女生和同学打架，老师让学生自主管理，发动全班同学投票，决定是让她留下学习还是请家长将其带走家庭教育一周。在得知自己被大部分同学投票赶走后，该少女留下遗言，投渠自杀。<br />
<br />
广西新闻网4月26日报道 15岁女生和同学打架，老师让学生自主管理，发动全班同学投票，决定是让她留下学习还是请家长将其带走家庭教育一周。在得知自己被大部分同学投票赶走后，15岁花季少女留下遗言，投渠自杀。<br />
<br />
学生自主管理的底线在哪里？一群未成年人能否决定一个未成年人的命运？15岁花季少女沉甸甸的生命拷问，让人无法逃避。<br />
<br />
被投票赶走后，她跳进水渠<br />
<br />
“雷梦佳生命就此结束！爸、妈，对不起，你们的恩情来世再报！辉辉，来世再做好朋友！”4月7日，在学校附近黄河渠边的青石板上留下三句遗言后，河南洛阳孟津西霞院初级中学初一学生雷梦佳投渠自尽。<br />
<br />
4月20日，雷梦佳的父亲雷先生告诉记者，4月10日，是雷梦佳的15岁生日，去年生日，她还请几个要好的朋友，撮了一顿以示庆祝。但今年的生日，没有了好朋友和父母的祝福，不知道她在另一个世界是否过得快乐？<br />
<br />
媒体报道，雷梦佳死于4月7日上午10时许。而在10时以前，雷梦佳被全班多数同学投票赶出了学校。<br />
<br />
全班投票的起因是雷梦佳犯了错。4月6日，雷梦佳和同年级其他班另一个女同学打架，班主任周老师在4月7日组织全体同学投票。投票之前，周老师让雷梦佳先回避，然后让全班同学就雷梦佳严重违反班纪班规的现象做了一个测评。测评是道选择题：是留下来给她一次改正错误的机会，还是让家长将其带走家庭教育一周。结果对雷梦佳不利。26个同学选择让她回家接受教育一周，12个同学选择再给她一次机会。<br />
<br />
根据多数学生的意见，周老师给雷梦佳母亲朱女士打电话，让她把孩子领走。对于老师的决定，朱女士说“自己并没有反对”，但当她赶到学校时，却发现自己的孩子不见了。<br />
<br />
律师：投票赶人，属于变相体罚<br />
<br />
作为当事方，雷梦佳的班主任周老师在接受采访时承认，投票决定雷梦佳的去留是事实，但孩子的死在他意料之外。周老师还透露，班上有规定，对犯错误的同学，学生要民主评议。<br />
<br />
4月15日，经孟津白鹤镇政府、学校和死者家属等五方多次协商，达成协议，由镇政府、村委会、西霞院初级中学、教师本人等给死者家属一次性补偿9万元。<br />
<br />
对老师的做法，雷先生认为，很不妥当。孩子再调皮，老师也不能组织同学赶她走，15岁的女生心理还未完全发育成熟，怎么能用这种极端的方式对待学生？<br />
<br />
雷先生说，雷梦佳平常性格很开朗，没有心理疾患。学习成绩在班里也不错，爱好文学，班主任老师平常对她也不错。老师如果是打她一顿，她也不会投河，关键是民主投票，伤了孩子自尊。<br />
<br />
4月14日，媒体报道雷梦佳的事后，孟津县教育局给洛阳市教育局递交了《关于白鹤镇西霞院初中学生雷某擅自离校后溺水身亡事件的调查及处理情况的报告》。4月15日，校方把补偿金给了雷梦佳的家人。<br />
<br />
雷梦佳的母亲朱女士不同意孩子“擅自离校”的说法。她说，是老师让孩子离校的。老师不把孩子亲手交给家长，这样做是不负责任的。<br />
<br />
洛阳洛太律师事务所张耀显律师说，民主投票赶学生走，属于变相体罚学生，和《未成年人保护法》精神相悖，但没有具体法律条文规定学校应该负多大的法律责任。<br />
<br />
当事老师神思恍惚，校长被停职<br />
<br />
4月20日，记者赶到西霞院初级中学，想搞清这次民主投票的前因后果。<br />
<br />
负责人李校长说，恳请媒体放过老师。因为目前电视、报纸、网络轮番上阵，矛头直指老师和学校。当事的周老师，目前神思恍惚，已在家休息。周老师家属找学校多次，说周老师现在精神状态很不好，实在不宜接受采访。<br />
<br />
李校长说，雷梦佳出事后，周老师就被教育局领导批评了，包括自己也被停职，目前自己是代理校长，眼下全校48名老师，人人自危，不敢管学生了。学校的教学管理工作面临崩溃。<br />
<br />
雷先生说，雷梦佳死后，周老师连道歉的话都没有。学校应该对全体老师进行再教育，以避免悲剧再发生在其他孩子身上。<br />
<br />
李校长说，不是不道歉，而是周老师的确受到巨大打击。从4月10日开始，学校已经对全体老师进行了四遍培训，下一步还要对老师进行再教育。<br />
<br />
对雷先生所称的雷梦佳性格很开朗，李校长说，的确是这样。<br />
<br />
李校长透露，对雷梦佳的死，雷先生曾向公安机关报案，学校倒是希望能查清真实死因，因为黄河渠的水平常比较浅，水位才到雷梦佳膝盖处，而雷梦佳出事那天，水位却上升了，这中间有何蹊跷，应该调查清楚，现在舆论一边倒地说学校逼死孩子，这不公平。<br />
<br />
不过，一位学校老师告诉记者，和雷梦佳打架的另一名女同学，也受到了老师批评，但没有遭受被同学投票赶走的命运。<br />
<br />
媒体和公众关注的焦点是，为什么要组织学生投票赶雷梦佳走？李校长说，县教育局曾开过研讨会，要求推广学生自主管理，没想到周老师让学生自主管理，却出了这事。<br />
<br />
雷梦佳被同学投票赶出校门，是不是学校自主管理的具体条文规定？李校长说，自主管理是新事物，教育部门和学校只是鼓励老师进行探索，学生违反课堂纪律，和同学打架，该给予什么样的处罚，该不该由学生投票决定，这些没有硬性具体规定。<br />
<br />
沉痛课题：学生需要什么样的自主管理？<br />
<br />
在西方的法庭审判中，经常使用陪审团制度，让公民决定公民的命运。学校或老师不能用这种方式管理学生吗？<br />
<br />
张耀显律师认为，陪审团制度是针对犯罪嫌疑人，而初一学生是未成年人，让一群未成年人决定另外一个未成年人的命运，是荒唐和不负责的行为，由此来剥夺孩子一周的教育权，也不符合法律规定。<br />
<br />
公开的媒体报道显示，学生自主管理在很多学校都已实行。三年前，漯河市一小学，为鼓励学生自主管理，设立了校长助理制度，小学生代替校长，检查校纪，对学生进行管理，受到了家长和老师的普遍好评。而焦作市沁阳市教育局2008年开始取消小学生家庭作业，让学生在业余时间自主管理自己，家长也很满意。<br />
<br />
但自主管理的“龙种”为何在雷梦佳的学校就成了“跳蚤”？陶行知先生曾撰文说，自主管理是为学生预备种种机会，使学生能够组织起来，养成他们自己管理自己的能力。苏联著名教育家苏霍姆林斯基在《给教师的一百条建议》中曾经说过：“自我教育需要有非常重要而强有力的促进因素――自尊心、自我尊重感、上进心……”郑州市教育学会会长陈明魁认为，西霞院初级中学采用伤害学生自尊心的方式，来进行学生自主管理、自我教育，如此南辕北辙的行为，不能和自主管理画等号。<br />
<br />
陈明魁说，中国内地，目前还没有哪一级教育部门公开推行学生自主管理制度，究竟学生该如何自主管理，底线和边界在哪里，还是一个正在探索的历史性课题。<br />
<br />
西霞院初级中学李校长说，农村学校老师的水平的确不如城里，这个悲剧，给学校提了个醒，希望全社会都来关注如何提高教育不发达地区教师素质问题，避免再次发生这样的悲剧。</div>

							
							
							
							
