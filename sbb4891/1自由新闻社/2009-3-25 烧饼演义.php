烧饼演义――前传 （<a href="http://www.bullogger.com/blogs/libertechine/archives/286719.aspx" target="_blank">http://www.bullogger.com/blogs/libertechine/archives/286719.aspx</a>）<br />
<br />
前传<br />
罐罐三鹿都是奶，奥运实在精彩。汉唐哪有和谐拽。<br />
访民依旧在，几度中南海。<br />
弱冠流离香街旁，惯看人权宪章。两声民主就结党。<br />
红朝多少事，不在史书上。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; ――调寄《临江仙》<br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;红朝开元以来，共立八世：太祖毛始皇、刘憨帝、华献帝、邓厉帝、胡德帝、赵愍帝、江僖帝、胡影帝。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;始皇开国以来，几行废立，太师刘憨公、中南王林幽公皆未得善终。后传位献帝，托孤于大司马兵部尚书叶定公。然献帝暗弱实难服众，定公摄政，生废立之意。是定公与右丞相邓怀公秘计，献帝乃退，怀公自立为厉帝。后三年，厉帝耄耋之躯行“领导干部革命化、年轻化、知识化、专业化”，立王储胡德公为帝，上皇垂帘听政。缘哿年，上以缘郾乒，黜之，立赵愍帝。愍帝三年，缘坜埃有民数万乱京师继而波及王土，唯松江平吐蕃定。六月，国人动乱，丞相李戾公奉上皇旨平定京师，废仁帝。松江巡抚江灵公即位，号僖帝。太上效法康熙点乾隆，又擢吐蕃总督胡和公入朝理事，封平王，官拜“红青团主”，敕设隔代接班人。僖帝七年，太上崩，帝独揽大权，从此华夏遍行贪腐之风。僖帝十年，有fa轮教作乱，内阁学士曾熙红献计除之，帝甚悦，拜红“阴国公”。次年，夷人美利坚于斯拉夫岛袭我使馆，诛我族人，举国甚怒，有民自焚于闹市，请战，帝不许。世皆怨帝之羸弱而思始皇邓厉之强硬。坊间乃传：储君平王监军吐蕃，料鹰派耳。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;即大位十二载，帝尽用松江旧部于朝野，人皆曰“松江帮”。然帝自视外臣，任京师朝臣结党营私，未敢与之争。其王孙尽享祖荫，终日放荡无度，鲜有成事者。世人皆以“公子党”称之。平王久据“红i团”，凡入团而有经纬者，平王皆敬而结之，曰：“我为太子，卿可助我图大业。”众人皆曰“苟富贵，无相忘。” <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;是年，帝年迈，欲传位于阴公。阴公少有权谋，好诗书，识廉理。其父乃开国元勋、户部侍郎曾海，故阴公自幼出入宫门，深得为官之术，不若其余公子党众。更兼得阴公早年官至松江布政使，盖亦同党耳。自那日定计除了fa轮教，帝视之为左膀右臂。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;话分两头，这平王寒门仕子出身，短短数年，从甘肃从七品布政使都事一路扶摇直上平步青云位极人臣。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;想那平王雄姿英发，风衣墨镜，谈笑间xueshan狮子旗灰飞烟灭激起千堆雪，文治武功，何其雄壮，有诗为证： <br />
<br />
马下提笔考清华，马上拿刀砍喇（空格）嘛。<br />
笔做刀来刀作笔，马匿草时草腻马。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;平王如此英武不凡，僖帝都不由得暗暗称奇。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;据信僖帝好娱乐，懂音律，通洋文。传世语录“too simple，sometimes naive”和“If you take care of your father，father will take care of you”享誉海外，为无数仁人志士所拜倒。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;厉帝在时，以定诏“废除领导终身制”，僖帝衰微之躯，上不能违天命，下不能背祖训。帝郁郁寡欢，终日不喜。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;阴公早察，谓帝曰：“远效康熙，近学厉帝则矣”…… <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;帝大喜，从其计。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;僖帝十三年，红朝百官聚首京师，推贤良、议废立。帝下诏退位，称太上皇，龙虎将军刘杭木奏请太上掌印信，收天下兵权，统帅三军。再举平王为影帝，前松江巡抚、御史大夫吴安国、前朝御史大夫温虚相为太师；又有直隶总督京兆尹贾熙林、大学士阴国公曾熙红任太保；后松江巡抚黄竹任太傅，鲁国公吴民正与太子太保、协办大学士罗操任左右都御史，广东巡抚李长冬为文渊阁大学士。以上诸人温太师原为赵愍帝门生，其余皆为太上心腹重臣。诸臣组阁，影帝甚为不悦。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;帝即大位，移驾直隶平山县西北坡村，观先帝毛始皇遗址，叹道：“朕未尝不思先帝之志，”遂命国中各人先后研习“两个务必”、“保先”及“八荣八耻”种种经史典籍，然民多愚钝，不得要领。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;行年,帝擢各省“红i团”大员，尽任总督巡抚布政司，把持从辽东中原到江浙一带的地方行政。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;同年，广东总督张道江入朝启奏，时有英伦弃民于港岛九龙冥顽不化，抗拒天恩王法。帝命九龙知府董立华、协办大学士礼部尚书唐国璇及温太师先后赴港，皆无功还。时有人保奏太保阴公可使之。帝准。阴公使而港乱定。帝大喜，封号国师，称“盛世第一能臣”。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;影帝二年，广东瘟疫传京师，太医郎中皆无良方。帝欲发兵去救，奈印信不掌，王师难发。屡请上皇，监医患于小汤山，广征良医，疫乃平。民多有怨者。帝不悦。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;国师献计：“今太上恃兵摄政久矣，吾有一计可除之。”遂献东厂密卷《松江贪腐录》一册，谓帝曰“可广告天下，太上结党营私亏空国库，民必恨之。是以反腐之名去其羽翼，上势孤，必可南面治之。”帝善其言，命吴罗左右都御使奉旨，领禁军三千拿太傅黄闵公妻子囚于文渊阁，曰“双规”――在规定时间、规定地点交代问题。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;不数日，太傅黄公抱病不朝。国师亲往太上府中细说厉害，太上惊，欲发兵讨之，恐生民怨。不日，国师奏言“上质六军而挟天子，欲效康熙点乾隆。”初，帝不应，后闻得上皇帐中三员一品猛将领侍卫内大臣龙虎将军刘杭木、掌銮仪卫事大臣金吾将军张约男与兵部尚书骠骑将军迟章曹列阵京郊，甚惧之。遂御驾京郊，从上皇言，定“京郊之盟”，准太上立储。僖帝欣然，退太上皇位，封沪国公，号“三表老人”，袭一等爵，回松江养老去了。至此，松江帮偶有余党立于朝堂，皆唯唯于帝耳。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;然国师率公子党纵横朝野，帝未敢与之争也。 <br />
<br />
<br />
<br />
<br />
烧饼演义 （<a href="http://www.bullogger.com/blogs/libertechine/archives/286819.aspx" target="_blank">http://www.bullogger.com/blogs/libertechine/archives/286819.aspx</a>）<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;红朝末年，影帝日衰。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;时年重聚百官，以立后事。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;帝擢辽国公李小强入朝，拟承帝位。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;这辽国公原本亦寒门仕子，第科举入燕园，为翰林院伺读学士，后入红i团，与帝共事厉、德、愍、僖四帝，有经天纬地之才。帝知其学高能重，甚爱之。后帝位三公，强领河南总督，封一等候。帝即位，强官居二品，封辽国公。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;择日临朝，朝臣奏请立储之事，内阁学士王深宁表辽公之德，众臣知宁乃影帝第一谋士，不敢妄言。唯国师道：“臣未敢忘‘京郊之盟’矣。” <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;帝视公子党众人皆有愠色，怒而不发，谓国师道：“爱卿意下若何？” <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;国师献太上意旨，道：“先皇在时，每言闽国公习不平大才。平乃御史大夫习成公之后。成公侍奉我朝始皇并憨、献、厉、德、愍、僖七帝，德才广布甘陕并东南各省，居功至伟；闽公耿忠良善，性宽仁厚。任福建、浙江两省总督时，勤政省心，颇有祖风。前松江巡抚陈善宇勾结商贾中饱私囊，交大理寺查办时，松江百姓皆有怨。闽公受命亲往抚之，泽被苍生，民莫不感其恩德。立闽公为太子，则我朝江山万世矣。”帝自思良久，徐许之，下诏立闽国公习不平为皇太子，册封平王，领武英殿大学士兼翰林院编修；立辽国公李小强为皇长子，领太傅兼御史大夫，助太师温虚相协理朝政。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;国师见太子当立，凑请告老还乡。众臣称善，不复言。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;话说这公子党中有一人，乃户部侍郎薄启来。来本是体仁阁大学士薄安公之后。安公行伍出身，随始皇倡义兵，监军陕甘，封开国广威将军。后弃武从文，官拜户部尚书。愍帝时，有八老摄政，安公首辅。幼时，来恃父威，性情放任，尤好犬马。少领辽东青泥洼知州，领能人巧匠开山石、兴土木，填东海以建新城，其城雄浑辉煌状若金山，固皆称“金州”。来又领诸匠于金州筑西洋音乐喷泉广场无数，世人皆赞“欧罗巴不能及也”。后擢辽东总督，再入京师，行走户部，官拜正二品侍郎。来此番入朝听封，以为升天，不想得巴国侯领江州巡抚，官至西南去了。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;来问阴公：“吾不及闽公乎？” <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;阴公道：“闽公少时，侍奉兵部尚书翟穆公，粗通兵法，公诚不及耳。” <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;来大怒而去。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;且说巴国侯帐中一谋士，侍来久矣，深知其志，进言曰：“世有一清虚道人，青城山张天师高足。后远赴西洋，深得道法，云游四海，行踪莫测。吾闻得此道近日在江州万县境内。公有大事，可亲往问之。”来善其言，当夜轻车简从，至万县。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;那道人见巴侯有齐天异相，以君臣之礼待之。来受之大喜。时来问卜，道人以指当空画圆，来不解其意，问之，道人不答，复画一圆；来仍不解其意，道人再画三圆乃去。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;侯惑而不言，随道人至后堂密室。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;道人谓巴侯曰：“小道不才，观侯有天子之相，斗胆代万姓启侯智。” <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;遂献天书《烧饼梦》十二卷，言：“参破此书者可得道。” <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;侯大喜，取天书一卷，看道： <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;（引子）开辟鸿蒙，谁为王种？都只为位高权重。趁着这解放天、改革日、和谐时，徒表愚忠。因此上，演这出你死我活的烧饼梦。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;（终身误）都道是和谐盛世，俺只念民主法制。空对着、金銮影帝折腾胡；终不忘、神州泪相千岁温。叹朝中、人言梨园今方服。纵然是防民之口，到底谁低俗！ <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;阅至此处，巴侯突发奇疾，胸中翻腾，肝胆欲裂，其状甚苦。道人急念咒救之，自思：“实非救国新主也。” <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;巴侯新愈，道人画一道鬼符放入锦囊，再取芝麻烧饼一只，空其内，将锦囊嵌入饼中，交予巴侯曰：“小道之前当空所画者，烧饼耳。此饼乃镇国之宝，持饼者有掌玺之运。” <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;侯既得此饼，大喜而去。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;早有厂公告于帝曰：“巴国侯结道议政，于空中画五环，人莫知其故。” 帝亦不解其意。有理藩院侍郎吴立民，早年使西洋，颇有才识，听闻五环，谓帝曰：“此乃夷人奥运盛会耳。”帝甚喜，命理藩院照会各国使节，次年八月奥运于京师。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;然帝知满朝文武鲜有善外事者，遂托太子平总理奥运，暗思“平若有失，朕必废之”。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;为行奥运，帝发民夫，征民膏，聘西洋工匠筑各式会馆，与京郊建大观园一座，名“奥运村”。不数日，立国技宫一座，名“有凤来仪”。又兼造“火立方”、“六颗松” 等等楼台若干。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;然自筹奥运以来，国事危矣。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;年初，南方大雪，国中道路结冰，有数百万乡民困他乡无还，帝祈天，太师下泪； <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;三月，吐蕃僧变，帝从内臣之计，内患外引，昭告天下：“此乃妖僧鸠摩智祸国耳”。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;四月，安徽童病，患五千余人；帝发五省良医救之； <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;五月，蜀地大陷，死伤数十万人。帝亲领六军去救，太师复下泪，谓灾民曰“多难兴邦”； <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;六月，贵州瓮安民变，有冤民聚数万焚县衙，贵州总督挥师平乱，言“乱民皆不明真相矣”； <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;朝中上下疲于奔命，更兼奥运临近，臣民具称红朝有亡国征兆耳。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;有臣启凑，言鸠摩智亡西域时，曾做妖法，咒我朝逢九必乱，今距妖僧西亡四十九年矣。请领中原高僧作法于朝堂，灾祸可免。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;帝素知鸠摩智本是宁夏人士，幼时赴吐蕃学法数十年，终得大果，自号“国师”。始皇在时，发兵征吐蕃，鸠摩智领众僧御天兵而不敌，败走天竺。其后四十九年，屡秘计独之，不得。鸠摩智屡游历西夷，以弘法之名传红朝暴政，恃精深佛法惑西夷之众。夷人敬之若佛家教皇，视红朝诸君为商纣隋炀。然红朝通商四海，西夷有求，怒而不言。 <br />
帝谓诸臣曰：“家奴作乱，王师可平；友邦反目，天威何存”，遂准，令五台山文殊院智幸方丈、普陀山普济院智裕方丈、九华山法华寺智望方丈及峨眉山报国寺智胜方丈入朝，弘法护国。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;自此，幸、裕、望、胜四大高僧齐集京师，作九九八十一天大法，保奥运安矣。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;时京师奥运，万国来朝，叹红朝雄壮，赞影帝圣明。帝大喜，表太子、太师、众高僧并文武百官之功，不在话下。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;然皇长子强恐太子平得势，不喜，谓帝曰：“若如此，儿无望矣。” <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;帝着心腹议之，王深宁献计：“夫天朝上国之君，理政于内，结夷于外，吾观辽公领御史大夫，多有劳苦，其功与太子筹奥运之功无异。今鸠摩智于海外诽议内事，夷对我朝颇有微词。可遣太子使之，太子不通外务，必出差错。” <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;帝善其言，使太子平于南美，至墨西哥，夷甚慢之，太子怒，责夷人曰：“世有夷人，饱而甚闲。我朝一不输革命，二不输饥贫，三不诽夷政，尔奈何群而谤我乎？” <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;帝喜，遂命人造势朝堂，责太子愚；又令侍卫扮作布衣平民，讥讽于市。自此太子势渐微矣。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;不数日，帝密招辽公入朝，商废立太子之事。辽止之曰：“平不足惧，公子党虽势众，但多不弄权谋，甘为商贾。然出入朝堂者尚有一人，除之，方可再图大计。”言毕，提笔写一“来”字，帝称善，废立之事少提。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;却说巴国侯领江州巡抚薄启来，自得了清虚道人之饼，不日加爵，号“渝国公”，大喜，于江州风水极盛之处石桥铺筑高台，曰“饼营”，日夜香火供奉。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;来听闻太子平失势，料鸿运至矣。为彰其志，令神乐署署丞录革命老歌廿八，传于江州小儿日夜诵之。世皆以为始皇复生。又常恨阴公言其不善兵事，差各州县衙役当街操演，凡遇蓄须散发鸡胸驼背痔疮脚癣体臭及走路外八字者，皆拿而问之。数日，囚者数万人。公笑曰：“江州安矣。” <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;时有历愍二帝京师之乱时所余反民，结“民主教”，奉夷人“普世价值”为尊，聚三百当世名士著《零八宪章》一卷，请改祖制，民多有从者。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;龙颜大怒，言民主邪教勾结乱党、私通外夷、祸乱朝纲，密诏凡著解贩印传抄者，皆为叛逆。遂差锦衣卫拿人，囚而用刑，以正视听。又于云南筑刑宫一座，名“看守所”，囚嫌与犯豺狼虎豹一室，人皆有去无还。典吏谓人曰：“此乃‘躲猫猫’耳。”民多有怨者。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;当是时，帝再施内患外引之计，复言“西洋有法兰西主欲会鸠摩智矣”。一时间海内山河震动，国人群起而攻，皆称吐蕃自古王土，夷不能图。如此三月，乱党诛而民怨平。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;各地上表请平乱之功。唯江州不报，帝奇之，问渝公。渝公笑答：“江州未乱。”愈奇之，复问，左右皆言渝公治民有方矣。帝大喜，封安王，总督西南诸省军政。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;时辽公生疑，密使人往江州，回报，具言清虚赐饼一事，辽公大惊，恐安王夺了上位，秘计除之。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;三月，太祖征吐蕃五十年矣，帝令各地设典纪念，各州府再征民膏，民愈恨。吐蕃各地密谋起事。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;是日，辽国公入朝，见影帝，言“儿臣有一箭双雕良计，可灭安王、平吐蕃，但乞父皇借三等侍卫一人则矣。” <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;影帝笑：“儿府中猛将百员亲兵数万，竟向朕求一区区三等侍卫。” <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;辽国公复言：“儿臣闻父皇早年平乱，于沙场拾得吐蕃遗孤，收为义子……” <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;帝大惊，问：“儿乞洛桑次仁何用？” <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;辽公笑而不言，帝素知其多谋，遂准。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;又十日，江州布政使王鸿擎飞书启奏，有吐蕃武士光天化日潜入石桥铺“饼营”，盗得宝饼，手刃护军，守备千总率外委把总追而不得，官民咸有惧色，皆称鸠摩智作乱。安王急火攻心，不省人事矣。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;又两日，辽国公李小强领洛桑次仁还朝，献宝饼，谓帝曰：“儿臣闻渝公得此饼时，道人曾告之曰‘此饼乃镇国之宝，持饼人有掌玺之运’。” <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;帝得饼而视之，饼上刻有夷文： <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;SB――dictatorship <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;着理藩院诸人录而译之，原是“独##裁烧饼”之意。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;又闻得清虚曾藏锦囊于饼中，帝亲启之，锦囊上一串小字“遇胡则开”。帝大喜，拆而视之，囊内羊皮一卷，录《烧饼歌》两首： <br />
<br />
一<br />
古道残月烙金帛，泪满长街又如何。<br />
日破云涛星火处，谁认他国作祖国。<br />
二<br />
欲雪家仇焚腊肉，又恐高声惊王侯。<br />
若承父辈文武志，定叫神州作金州。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;当是时，帝头痛剧烈，忽见清虚道长跃然金銮殿上，帝大惊，令三营禁军死命拿之不得，吐血数斗，不复临朝矣。<br />
