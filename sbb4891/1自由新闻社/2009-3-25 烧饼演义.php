东方朔：汉代就有上环手术<br />
文/醒着的和梦着的<br />
<a href="http://www.tianya.cn/publicforum/content/no01/1/267327.shtml" target="_blank">http://www.tianya.cn/publicforum/content/no01/1/267327.shtml</a><br />
近来心绪颇不宁静，唯有读古书抄古碑以度日。日前翻阅东方朔笔记《异冤录》，忽见一则异闻，意味深长，录来以博众读者一叹。<br />
　　 《异冤录》中有一篇《镶环避奸》，内容如下：<br />
　　古鄂州某郡名想凡者，举郡男女皆美俊，即令上方神仙，见之亦动凡心，故名。国朝官宦，皆欲谋守其郡，以亲近芳泽者。其郡男女，皆畏为狼虎而莫可奈何。五十三年，有姓猢名虎者守其郡，大置媵妾，又狎人妻女，民避之如祸。一日，猢虎饮于有玉楼，见有民女中飞飞者，貌胜天仙，因强留饮求欢，遭拒，以力淫之。中飞飞，性烈女也，暗思遭此玷辱，必留孽胎，遂自9楼投地而死。郡人闻之，家有余赀者皆于医，于妻女体内镶一金环，以避遭辱而孕。自此，郡人皆自谓镶环。后有士人，以此谓不雅，遂改称镶为襄，易环为樊。环者，鄂俗音同樊。<br />
　　 原文文言难懂，醒着试翻译于是：<br />
　　 古代鄂州有一个名叫想凡的郡（相当于今天的地区），因为全群里都是俊男美女，即使是天上的神仙，见了他们也都会动凡心，因此得名。我大汉朝的官员们，都想到想凡郡去做郡守，以便能与当地的美女们亲近一番。当地的人们，都像狼虎一样害怕这些官员，却没有一点儿办法。大汉53年，有一个名叫猢虎的人成为想凡的郡守，到任之后大量纳妾，又随意调戏人民的妻子和女儿，百姓都像躲祸一样躲着他。一天，猢虎到有玉楼酒家喝酒，看见一个名叫中飞飞的女孩子，貌若天仙，于是强行留下她一同喝酒，又想和她“上床“（注：欢用现代汉语难以翻译，勉强译为上床吧），遭到少女的拒绝，便使用武力将她奸淫了。中飞飞是个个性非常强的女孩子，她想，遭此奸淫后，必定会怀孕，于是从9楼上跳下来自杀了。凡者郡人知道这件事后，有钱的人家纷纷到医生那里，请求在他们妻子或女儿体上镶上一个金属环，以避免遭到奸淫后怀孕。从此之后，该郡人都把当地叫做镶环。后来，有读书人认为这个称谓不雅，便将“镶”改为“襄”，“环”换作“樊”。“环”这个字，在鄂州与“樊”是同音的。<br />
　　<br />
　　 真是奇怪啊，难道汉朝就有上环避孕的技术吗？<br />
