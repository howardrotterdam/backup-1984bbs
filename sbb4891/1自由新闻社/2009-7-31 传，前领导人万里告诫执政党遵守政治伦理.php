央视国际 2004年09月07日 14:13<br />
<br />
　　“我差点被当成杀人犯”，沈志华回忆起30多年前自己的“档案事故”，仍心有余悸。那时他是部队里的技术尖子，却始终入不了党，最后被勒令复员。当时，团长对他说了这样一句话，“现在我不能跟你说是怎么回事，但是你留在部队里是没有前途的，还是回到地方发展吧。” <br />
<br />
　　后来，指导员到北京出差，终于忍不住吐露了真相，“看你这么傻乎乎地蒙在鼓里，我真是不忍心，如果你档案里的那份东西不拿掉，你永远都没有前途。”原来有人揭发沈志华曾经杀过人，部队虽然查无实据，但却将这份材料放进了他的档案。 <br />
<br />
　　沈志华根据指导员提供的线索，找到了揭发者，问“你认识我吗”？对方说不认识。表明身份后，对方承认自己是为了积极表现，瞎编的。沈志华让他写了证明材料，这才把“杀人材料”从档案里抽了出来。 <br />
<br />
　　“如果我找不到那个人，我这辈子冤不冤？都不知道为什么？”说起此事，沈志华至今还愤怒不已。 <br />
<br />
　　“杀人”事情虽然抹平了，但档案的风波还没有完。粉碎四人帮前夕，他被莫名其妙地戴上了反革命帽子，被投入监牢；第一年恢复高考，又因为档案政审不合格，失去上大学的机会；以后破格考上了中国社科院世界史专业的研究生，又不能够毕业。相当长的一段时间，沈志华的档案无人接收。 <br />
<br />
　　沈志华自嘲自己的前半生真是倒霉透顶。他成为了无业人员，不得不下海，好在从商的运道还不错，他成了一名富裕的书商。 <br />
<br />
　　1992年，已是成功商人的沈志华突然结束了自己的生意，开始研究国际史。他把自己的研究领域定位在苏联史、冷战史和中苏关系，并把大量精力放在解密档案的搜集上。 <br />
<br />
　　沈志华说，如果把历史研究者看作厨师，档案就是食粮，没有一手档案，难为无米之炊。 <br />
<br />
　　1996年至2002年间，沈志华与社科院合作，个人出资140万元，专程到俄罗斯和美国搜集俄国解密档案，并组织翻译、整理、出版了36卷《苏联历史档案选编》。 <br />
<br />
　　档案搜集的过程让沈志华感慨万千。第一次到俄国查阅档案时，常常是到小窗口先递上介绍信，等两个小时后，被告知今天看不了。后来沈志华发现俄罗斯对自己人要宽容得多，就委托俄国同行查，给每人几百美金作为劳务费，有效率还省钱。沈志华的商人天赋，不经意地流露出来。 <br />
<br />
　　1997年，沈志华飞到美国查找档案时，感觉顺利多了。在那里，只要能够开放的档案，不仅一路绿灯，还有志愿者服务。又方便又快捷。 <br />
<br />
　　不管是受到冷遇还是周到服务，沈志华都在国外达到了自己的搜集目的，最令沈志华挠头的，反而是在自己家门口查档案。开国际会议的时候，他常常被问到，你研究中苏历史，为什么只有苏联的档案，没有中国的档案？他无言以对。他曾经去过中央档案馆三次，手续复杂不说，连个纸片也没有见着。一次想查找苏联专家的资料，向一个部委的档案馆求助，被对方干脆地拒绝说，不向外人开放。沈志华问什么叫外人，对方回答：该部委以外的人都是外人。 <br />
<br />
　　沈志华又自费沿中苏、中蒙边界跑了十多个省包括重点市的档案馆，“为什么跑那么多省，就是因为在上面看不到，只能去地方看。”然而，即便到地方，他也不一定有收获，这完全要看当地档案馆的具体执行人。千里迢迢赶过去，有的可以随便看，有的单位连门都不让进。最令他焦心的是有时要查找机密一些的档案，不得不先让领导审，一审就是三天，赶上领导开会、出差的话，等待的时间更长。 <br />
<br />
　　在沈志华家里，专门腾出两个房间，搁放搜集到的两万多件档案，“在苏联冷战史这方面，我的档案全国最全。”他不无自豪地说。许多人做博士论文，专程到沈志华家查找资料，甚至韩国、美国等地的学者都远道而来求助。中国社科院一位苏联史专家认为，有了这批档案，不仅苏联史要重新认识、重新书写，对20世纪世界史和国际关系史都要重新思考。 <br />
<br />
　　2004年1月，中国外交部部分档案公开解密，第一拨就赶去查阅的沈志华说，“非常惊喜”。（新闻周刊） <br />
