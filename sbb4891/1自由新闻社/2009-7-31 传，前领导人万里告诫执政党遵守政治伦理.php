													<h2>老同志关于政治伦理的文章，有两点我八卦一下</h2>
						
						
																											<div id="postmessage_187483" class="t_msgfont">关于《历史的先声》这本书：<br />
1999年9月 ，笑蜀将1949年中共夺取政权以前该党的 《新华日报》和《解放日报》上主张民主自由、民主、平等的文章汇集成册，取名为《历史的先声-半个世纪前的庄严承诺》，作为“草原部落黑马文丛”之一，由汕头大学出版社出版。 <br />
　　 <br />
　　 也许有读者对《新华日报》和《解放日报》陌生，在此简单介绍一下。《解放日报》创刊于1941年共产党延安时期，是中共早期的政治理论刊物，建国后是中国上海解放日报报业集团出版的一份日报，现为中共上海市委的机关报。《新华日报》（1938.1.11-1947.2.28），原中共机关报，在抗日战争爆发后成为共产党在国统区合法出版的刊物，并持续至国共内战爆发，1947.2.28被国民党下令取消。现为中共江苏省委机关报。在国统区的八年期间，有时稿子被扣，但可以刊登启示或“开天窗”，用以说明稿子被国民党新闻检查机关扣发。这两大报纸上的文章多是中共在与国民党争夺政权的斗争中，由共产党领导人或理论家亲自撰写的。这些文章反对国民党的“一党专政”，控制新闻舆论，破坏平等民主自由等。 <br />
　　 <br />
　　 1945年中国取得对日的“惨胜”，国共两党在“妥协”失败后，面临得是“惨斗”。1945年10月1日，国民党政府取消新闻检查制度，这是《新华日报》和《解放日报》的一个转折点，意即取消前和取消后，它们的“话语目标”与“话语方式”是不同的，这可以从当时的相关文章的标题看出。比如抗日战争期间，发表《建立宪政规模》（1939.9.21）《不能因国民程度不高而拒绝民主》（1939.12.25）《论学术自由》（1942.10.11，）《言论自由与民主》（1944.4.19）《新闻民主-民主的基础》（1945.3.31）《以出版自由繁荣出版业 接触精神匮乏的不二法门》（1945.6.26）等等呼吁民主与自由的文章；取消新闻检查制度后，《新华日报》迎来了它的“黄金时期”，发表《反动派也该想一想，人民岂可轻侮》（1945.12.9）《民主一日不实现 中国学生运动一日不停止》（1945.12.9）《希特勒的法宝应该收起了》（1945.12.4）《报纸应各处专制主义者 不许人民说话和造谣欺骗人民的歪风》（1946.1.11）《一党独裁，遍地是灾！》（1946.3.30）《谁使中国不能安定？专制政府！》（1946.5.17）等一系列文章。以上所摘文章均来自《新华日报》。 <br />
　　 <br />
　　 1945年美国报纸主笔协会（ASNE)代表团，根据国会通过的议案，游历欧亚，呼吁各国将新闻自由列入战后和平条约。3月28日，福勒斯特、麦吉尔、亚更曼到达中国。4月1日晚，麦吉尔访问中共的《新华日报》，看到一些被删得支离破碎的送检稿件，十分惊异。4月2日，福勒斯特等人受到蒋介石的接见。中央社报道“美国新闻界代表团普谒蒋主席报告任务”，“畅谈约一小时，始兴而出”，那谈了什么呢？据钱钢查这次谈话的记录，载于1984年（蒋死后第九年）由国民党史委员会编辑的《总统蒋公思想言论总集》。该书记录“委座”的谈话：“余对新闻自由之原则，甚表赞同。”引起钱钢注意的：一、福勒斯特谈到“现在苏联对外宣传，至为积极”；二、亚更曼建议将大力发展广播，作为防御外来宣传的武器；三、麦吉尔认为“中国政府让中共说话之机会太多”，“似应在中外报纸披露中共种种事实，使中外人士明了中共之真相”；四、蒋介石斥责，“中共之一切宣传，借为虚伪的、欺骗的”。钱钢认为国民党方面的记录，可能突出了对自己有利的部分，但伪造的可能性不大。这份记录当时没有公布，否则将是中共抨击国民党的重要证据。 <br />
　　 <br />
　　 对于新闻自由，中共领导们又是如何看待的呢？毛泽东与其秘书林克有过一段谈话：“主张新闻自由实际上与上大街（注：游行示威）差不多。”又中共中央政治局常委陈云的一段话：“在国民党统治时期，制定了一个新闻法，我们共产党热闹仔细研究他的字句，抓它的辫子，钻它的空子。现在我们当权，我看还是不要新闻法好，免得人家钻我们的空子。没有新闻法，我们主动，想怎样控制就怎样控制。” <br />
　　 <br />
　　 政治的游戏，历史的诡异，由此可见。 颇具讽刺意味的是，《历史的先声-半个世纪前的庄严承诺》一书出版后，中宣部与新闻出版总署专门就此书开了一个会议，认为此书是“借我党领导人早期批评国民党的文章攻击我党与政府，居心叵测”，“应当严厉禁止今后再出版这类借历史影射攻击现实的书”。出版该书的汕头大学出版社因此被停业整顿，编者笑蜀也被迫离开原来任教的大学。 <br />
