													<h2>江苏常州女医生开一毛钱处方将病人治愈</h2>
						
						
																											<div id="postmessage_667984" class="t_msgfont">江苏常州女医生开一毛钱处方将病人治愈(图)<br />
<br />
2010-06-09 04:54:39　<br />
<br />
来源: 扬子晚报　<br />
<br />
核心提示：江苏常州一位市民到医院为孩子看拉肚子，医生只开了一毛钱的药，就治好了孩子的病。令家人十分感慨。<br />
<br />
<br />
<br />
<br />
扬子晚报6月9日报道 如今一毛钱能买到什么？常州一位市民近日在市内一家医院给孩子看病时，仅花了一毛钱，孩子的病就痊愈了。这个一毛钱处方被病人家属誉为“世界上最便宜的处方。”<br />
问诊后，医生只开了5片药<br />
在常州市里的医院看病，药价居然只要1毛钱！这么低廉有效的处方大大出乎常州市民张先生的意料。昨天上午，张先生告诉记者，6月2日，他带着孙女去常州第七人民医院看病时遇到了这样的好事，不仅钱花得少，服过一毛钱的处方药后，孙女的病很快好转了。<br />
张先生介绍说，孙女小月今年16个月大，胖乎乎的十分招人喜爱。就在5月31日的夜里，小月突然莫名其妙地拉肚子，到1日下午已经拉了六次。看着天真可爱的小宝贝没有了往日活泼，一个劲地撇着小嘴哭闹，尿布和裤子脏了好几次，全家人都为此焦躁不安。<br />
2日一大早，他就喂孙女吃药，并两次将孙女的大便送去一家医院检查。医生说查出来有细菌感染，医生开了头孢克肟等，并说吃药效果不明显，必须要挂盐水方能见效。<br />
2日下午四时，张先生和老伴决定送孩子去医院挂水。“根据经验，我们带了500元。”张先生介绍，他们带着孙女来到常州第七医院儿科门诊。轮到小月时，接诊的徐医生详细问了病情，看了在别的医院的大便化验单，徐医生告诉他们说，这次孩子拉肚子是细菌感染，现在没有热度，就暂时用不着挂水，先吃吃药再说。<br />
“听到孩子暂时用不着挂水，我们觉得轻松一大半了。”张先生说，徐医生在病历上写了三种药，知道家里已有两种后，徐医生便在处方单上只开了“痢特灵”一种药。<br />
当他拿着药方去窗口付款时，收款小姐在里面说了一句“一毛钱”。“我还以为自己听错了，而第二遍听到‘一毛钱’时，我还将信将疑。”张先生说，收款小姐又说了一句：“一毛钱！”张先生说，当时就显得不知所措，竟然只要一毛钱，他先是愣了一下，付了一毛钱，到领药窗口去领了一小包药，里面是5粒黄色小药片。回来后，徐医生嘱咐他们，痢特灵半粒一顿，一日三顿，5片药可吃三天了。<br />
孩子病好了，家长很感动<br />
“要不是我亲身经历，我怎么也不相信看病只要花一毛钱。” 张先生和老伴很有些感慨。一毛钱的药方也恐怕是当今世界上花钱最少的处方，特别是当今看病难、看病贵的今天，实在是颇为鲜见。现在小孙女小月的病痊愈了，家人感到庆幸的同时，也有一股感动。<br />
昨天上午，在常州市第七人民医院儿科门诊，记者见到了正在忙碌的徐莉医生，她正在为一名发烧儿童诊断，一边仔细地询问，一边不停地在病历上记录。由于排队看病的病人很多，徐医生几乎没有闲下来的时间。“上次我所开出的一毛钱处方的确有此事，但这是应该的，没有什么大惊小怪的。”在工作间隙，徐医生简单地表示，平时来她这里看病的除了市民外，还有不少外来工及其子女，他们的生活都不容易。医生应该对每一个病人负责，根据病情开药，多开药不见得就能把病看好，对病人来说也是一种负担。<br />
<br />
<br />
该院宣传科的刘科长称，一毛钱处方是个例，徐医生不是对每个病人都开一毛钱的处方，收一毛钱是因为这位家长来他们医院之前已经在另一家看过病。徐莉目前是医院的儿科主任，许多患者指名要她看病。她把自己的手机号码留给病人，一旦有什么事情随时都能找她。有些病人不惜排队等上两三个小时，也要等她。即使徐医生中午下班的时候，也有病人家属在等她看病，多的时候一天八九十个病人，少的时候一天也有六七十个病人前来找徐医生看病。<br />
据院方介绍，医院提倡“合理检查、合理治疗、合理用药”，对医生的考核更注重的是工作量、服务质量等方面的综合考评。医院对医生用药不作考核，医生的综合工作效率才是医院最为看重的。<br />
