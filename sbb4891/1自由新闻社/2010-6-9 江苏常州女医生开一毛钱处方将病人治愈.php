													<h2>专业的角度说几句</h2>
						
						
																											<div id="postmessage_668881" class="t_msgfont">不想花钱看好病是每一个病人的心愿，但是为了达到这个目的，途径却是异常艰难。<br />
最简单的办法，就是自己懂医学，虽然俗话说：“医者不自医”，但是实际上，医生自己给自己开处方，不禁比比皆是，而且异常方便，可以选择最便宜，疗效最好的药物。当年在医学院的时候，校医基本不说“有什么不舒服”，常常直接问：“你要开什么药？”<br />
如果自己不是医生，也不准备花费至少5年时间学习医学，也不准备侮辱自己智商相信茄子、绿豆之类的东西，最简单的方法就是认识一个医生朋友吧，关系越亲密越好，要关系好到足以使人情成本大于多开药的收益，说明详见李清晨所著《医生的困境》：<a href="http://songshuhui.net/archives/16958.html" target="_blank">http://songshuhui.net/archives/16958.html</a>。在这个前提下，找医生做伴侣，当亲戚，结拜兄弟如此等等，是最简单的办法。<br />
如果还是不行，那就前往社会福利好的欧洲国家吧，哪里看病基本免费，只要你愿意承担看门诊须提前预约几个月还有至少占三分之一收入的税收。<br />
――――――――――――――――――我是猥琐的分割线――――――――――――――――――――――――――――――<br />
以上是从病人角度分析为什么廉价看病在现实生活中少见，以下是开廉价药物的现实困境和经济学分析：<br />
现实中确实有许多药物作为国家基本药物，一般疗效确切，价格低廉到可以忽略不计，比如几分钱的叶酸片，还有几毛钱的肾上腺素针，地高辛片，几块钱的廉价抗生素比比皆是，包括在原文中提到的止泻药。这些药物无一例外都是国产药，是国家按照计划经济的模式在药厂定点生产的，而且定价几十年不变，还强制生产，结果药厂生产越多，亏损越多，为了较少亏损，没有人会大量生产，而且因为利润太低（常常是负利润），质量也靠不住。（参见上海华联制药厂事件：传送门：<a href="http://finance.sina.com.cn/consume/puguangtai/20071213/07184285258.shtml" target="_blank">http://finance.sina.com.cn/consu ... 3/07184285258.shtml</a>；齐二药厂假药“亮菌甲素”事件：<a href="http://news.xinhuanet.com/politics/2006-05/25/content_4597178.htm" target="_blank">http://news.xinhuanet.com/politics/2006-05/25/content_4597178.htm</a>）事实上这样的事情发生太多了，更多的事件不会通过媒体，只要范围不大，处理只是在卫生系统内部发个通知，然后药房撤药了事。你说作为医生，这样的药你敢用吗？作为病人，一种国产药几块钱，进口的几十块钱，难道你会为了几十元钱承担未知风险？<br />
作为医院，要知道目前最好的公立医院财政拨款也不足30％，为了解决困境，医院广泛使用科室承包和成本结算的办法控制成本，要求临床科室制造利润，至少是收支平衡，要是亏本，很简单，减少该科室规模，甚至是撤销科室就可以了，因为医院需要买药来生存。这不是危言耸听，在很多地方已经出现了招不到儿科医生和儿科撤销的情况，无他，无油水且风险大。我认为医疗改革最失败就在于过度市场化，医药分离嚷嚷了几十年，但是财政投入没有明显增加，怎么可能做到。广大人民群众不动脑子，强制医生开几分钱的药确实很痛快，但是医院倒闭了，医生转行了，网民们难道自己给自己看病开药吗？<br />
最后最为医生，他们都是人，很多都经过了5年、7年、甚至10年的专业培训，医学教育需要高素质的人才，长学制的培养，毕业都是快30的人了，和所有人一样，他们也需要买房买车，结婚生子，赡养父母，有的还需要偿还读书期间的债务，但是收入是多少？上海最好的医院硕士生账面收入是3000元，中高年资医师账面收入数千到1万不等，试问为了生存，为了高质量的生活，换作是你，你有多么大的毅力能抵制住自己不收红包回扣，这个行业的潜规则，而且几乎没有被查处的风险。<br />
医生开处方的时候，用什么药物，使用多久，往往只在他的一念间，几分钱的药确实有可能看好病，不做检查很多时候也没什么大问题，但是万一，只怕万一，有病人借口因为药物质量差或者检查不完善找麻烦的时候，医生只会因此吃亏、乖乖赔钱，而开高档药、开大化验单，实际上是自我保护的最好办法。还是那句话，想让医生担风险，那是需要条件滴。<br />
