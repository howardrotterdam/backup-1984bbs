													<h2>杭州“5・7”交通肇事案公开宣判 胡斌被判三年</h2>
						
						
																											<div id="postmessage_170650" class="t_msgfont">7月20日，被告人胡斌正在被告席上接受审判。新华社记者王定昶 摄<br />
<br />
<br />
遇难者浙江大学毕业生谭卓 (资料图)<br />
中新网杭州7月20日电7月20日下午3时30分，杭州市西湖区人民法院对“5&#8226;7”交通肇事案进行了一审公开宣判，以交通肇事罪判处被告人胡斌有期徒刑三年。被告人胡斌亲属，被害人谭卓亲属、生前同事等各界群众60余人旁听了宣判。<br />
<br />
杭州市西湖区人民检察院于2009年7月3日向西湖区人民法院提起公诉。西湖区人民法院依法组成合议庭，于7月15日公开开庭审理了本案。法院经审理查明，2009年5月7日晚，被告人胡斌驾驶经非法改装的浙A608Z0蓝瑟翼豪陆神牌红色三菱轿车，与同伴驾驶的车辆从杭州市江干区机场路出发，前往西湖区文二西路西城广场，想看看该广场是否还在放映名为《金钱帝国》的电影。在途经文晖路、文三路、古翠路、文二西路路段时，被告人胡斌与同伴严重超速行驶并时有互相追赶的情形。当晚20时08分，被告人胡斌驾驶车辆至文二西路德加公寓西区大门口人行横道时，未注意观察路面行人动态，致使车头右前端撞上正在人行横道上由南向北行走的男青年谭卓。谭卓被撞弹起，落下时头部先撞上该轿车前挡风玻璃，再跌至地面。事发后，胡斌立即拨打120急救电话和122交通事故报警电话。谭卓经送医院抢救无效，于当晚20时55分因颅脑损伤而死亡。事发路段标明限速为每小时50公里。经鉴定，胡斌当时的行车速度在每小时84.1至101.2公里之间，对事故负全部责任。<br />
<br />
另查明，案发后胡斌亲属与被害人亲属已就民事赔偿达成协议，胡斌亲属已赔偿并自愿补偿被害人亲属经济损失共计人民币1 130 100元。<br />
<br />
法院认为，被告人胡斌违反道路交通安全法规，驾驶机动车辆在城市道路上严重超速行驶，造成一人死亡并负事故全部责任，其行为构成交通肇事罪。被告人胡斌肇事后及时报警并在现场等候，该行为属于履行道路交通安全法规定的义务，且刑法已将交通肇事后逃逸的行为规定为加重处罚情节，依法不应当将肇事后报警并在现场等候的行为重复评价为自动投案，故不能认定被告人胡斌有自首情节。被告人胡斌案发后虽未逃避法律追究，其亲属也能积极赔偿被害人家属的经济损失。但胡斌无视交通法规，案发时驾驶非法改装的车辆在城市主要道路上严重超速行驶，沿途时而与同伴相互追赶，在住宅密集区域的人行横道上肇事并致人死亡，造成恶劣的社会影响，犯罪情节严重，应从重处罚。但被告人胡斌的行为不符合最高人民法院《关于审理交通肇事刑事案件具体应用法律若干问题的解释》第四条关于交通肇事罪其他特别恶劣情节的具体规定，认定其有其他特别恶劣情节缺乏法律依据。<br />
<br />
法院据此依照《中华人民共和国刑法》第一百三十三条、第六十一条和最高人民法院《关于审理交通肇事刑事案件具体应用法律若干问题的解释》第二条第一款第(一)项之规定，作出上述一审判决。<br />
<br />
宣判后，该院随即召开了新闻发布会，该案审判长通报了“5&#8226;7”交通肇事案件的一审审理情况。人民日报、新华社、中新社、人民法院报、浙江日报、杭州日报、浙江卫视、杭州电视台综合频道等30余家媒体记者参加了新闻发布会。<br />
