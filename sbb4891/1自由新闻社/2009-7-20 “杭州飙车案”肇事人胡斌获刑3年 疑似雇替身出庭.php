审判长详解审判结果提出三条理由<br />
日期：2009-07-21 作者：方益波;余靖静 来源：新华社 <br />
&nbsp; &nbsp; 新华社杭州7月20日电&nbsp;&nbsp;（记者方益波&nbsp;&nbsp;余靖静）被社会舆论称为“杭州飙车案”的杭州“5・7”交通肇事案今日宣判，被告人胡斌被以交通肇事罪一审判处有期徒刑三年，审判长潘波在宣判后的新闻发布会上，就庭审争议的焦点和社会舆论关注的问题详细解释了有关法律依据，陈述了判决理由。<br />
为啥以交通肇事罪定罪<br />
&nbsp; &nbsp; 检察机关以交通肇事罪起诉，庭审中被害人诉讼代理人、被告人辩护人对此均不持异议，而一些社会舆论认为胡斌的行为构成以危险方法危害公共安全罪或者过失以危险方法危害公共安全罪。<br />
&nbsp; &nbsp; 潘波说，法院之所以以交通肇事罪对被告人胡斌定罪，理由是：<br />
&nbsp; &nbsp; 第一，以危险方法危害公共安全罪、过失以危险方法危害公共安全罪与交通肇事罪侵犯的客体都是公共安全，客观方面均表现为实施了危害公共安全的行为，因此，交通肇事罪本身就是一种危害公共安全的犯罪。<br />
&nbsp; &nbsp; 第二，以危险方法危害公共安全罪是故意犯罪，行为人的主观心态是故意，是希望或者放任危害社会结果的发生；而交通肇事罪的主观方面是过失，包括过于自信的过失和疏忽大意的过失，即应当预见到自己的行为可能发生危害社会的结果，因为疏忽大意而没有预见或已经预见而轻信能够避免。根据庭审查明的事实，被告人胡斌平时喜欢开快车，但其认为凭自己的驾驶技术能够避免事故的发生，案发当晚，胡斌在超速驾车过程中未违反交通信号灯指令，遇红灯时能够停车，肇事时没有注意观察前方路面情况而撞上在人行横道上行走的男青年谭卓，撞人后立即踩刹车并下车查看谭卓的伤势情况，随即拨打了120急救电话以及122报警电话，并留在现场等候处理。这一系列行为反映了胡斌肇事时主观上既不希望事故发生，也不放任事故的发生，对被害人谭卓的死亡其内心是持否定和排斥态度，是一种过失的心态，因此，被告人胡斌的行为不构成以危险方法危害公共安全罪。<br />
&nbsp; &nbsp; 第三，过失以危险方法危害公共安全罪和交通肇事罪的主观心态都是过失，但是我国刑法分则第二章危害公共安全罪中对违反交通运输管理法规，发生重大事故，致人死亡的，刑法第133条专门规定了交通肇事罪，因此，驾驶交通工具在公共交通管理的范围内过失致人死亡的，应认定为交通肇事罪，而不能适用刑法第115条第2款规定以过失以危险方法危害公共安全罪定罪。根据罪刑法定原则，法院以交通肇事罪对被告人胡斌定罪。<br />
<br />
算不算“有其他特别恶劣情节”<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 庭审中被害人诉讼代理人提出胡斌有其他特别恶劣情节，法院没有采纳代理人意见。潘波说，依据就是最高人民法院的司法解释。<br />
&nbsp; &nbsp; 潘波解释说，2000年11月最高人民法院《关于审理交通肇事刑事案件具体应用法律若干问题的解释》第四条规定，交通肇事具有下列情形之一的，属于“有其他特别恶劣情节”，处三年以上七年以下有期徒刑：1、死亡二人或者重伤五人以上，负事故全部或者主要责任的；2、死亡六人以上，负事故同等责任的；3、造成公共财产或者他人财产直接损失，负事故全部或者主要责任，无能力赔偿数额在六十万元以上的。<br />
&nbsp; &nbsp; 他认为，据此，胡斌的行为不符合司法解释关于交通肇事罪其他特别恶劣情节的具体规定，根据罪刑法定原则，也不能认定被告人胡斌交通肇事有其他特别恶劣情节。<br />
<br />
胡斌“有自首情节”吗<br />
&nbsp; &nbsp; 胡斌的辩护人在庭审中提出胡斌有自首情节的意见，没有被法院采纳。<br />
&nbsp; &nbsp; 潘波说，这是因为被告人胡斌肇事后及时报警并在现场等候的行为，属于履行道路交通安全法规定的义务，刑法对交通肇事后逃逸的行为规定为加重处罚情节，刑事立法上已将履行道路交通安全法规定义务的肇事人予以从轻处罚。如果将被告人胡斌肇事后报警并在现场等候处理这种履行法定义务的行为认定为自首情节并再次从轻或者减轻处罚，那在法律上就进行了重复评价。<br />
&nbsp; &nbsp; 他认为，如果某个人交通肇事后逃逸，之后又到司法机关投案，还是可以认定为自首的，但是刑法第133条明确规定，肇事后逃逸应在三至七年的幅度内量刑，对肇事后逃逸的行为人即使有自首情节也必须在三至七年的幅度内综合考虑是否从轻或者减轻处罚。因此，刑法对肇事后逃逸再自首的行为人的处刑仍比肇事后履行报警等法定义务的行为人要重。<br />
为什么刑期是三年<br />
&nbsp; &nbsp; 潘波说，根据刑法第133条规定，对胡斌交通肇事行为应处三年以下有期徒刑或者拘役。案发后胡斌亲属积极赔偿了被害人家属的经济损失，最高人民法院《关于刑事附带民事诉讼范围问题的规定》第四条规定，被告人已经赔偿被害人物质损失的，人民法院可以作为量刑情节予以考虑。<br />
&nbsp; &nbsp; 他强调指出，但是胡斌无视交通法规，案发时驾驶非法改装的车辆在城市主要道路上严重超速行驶，沿途时而与同伴相互追赶，在住宅密集区域的人行横道上肇事并致人死亡，造成恶劣的社会影响，犯罪情节严重，应从重处罚。故积极赔偿被害人经济损失等理由尚不足以减轻其罪责。辩护人还在庭审中提到胡斌曾在体育比赛中获奖的意见等，这些更不能成为从轻处罚的依据。</div>

							
							
							
							
