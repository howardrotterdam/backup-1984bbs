我爸被困在街对面过不来<br />
2009-08-28 23:44:50<br />
<br />
现在是北京时间23：29分，据前方记者（我爸）的最新报道，长安街和周围大部分街道已被封死，公交车、地铁停运，出租车一个也见不着。街道两旁布满临时厕所、警察和武警。<br />
<br />
我爸很悲惨地在单位加班到晚上10点，出门之后发现自己中了头奖，碰上60年一遇的国庆阅兵预演，因此无法过街回到街对面的家里。现在他仍被困在王府井附近，并努力地用双脚向北转移，希望能找到一条没被封的路，然后努力地绕个超级大弯子回家。不过目前情势非常不妙，他极有可能在发现即使能绕回家也到早上上班时间了的情况下迫不得已留宿街北的某不知名小旅馆。<br />
<br />
我刚给他出主意说，让他就地犯罪，然后被警车拉到西城分局，这样就能顺利回家了。但此馊提议已被他否决。<br />
<br />
是上天弄人。这件事本该发生在26号晚上，七夕。我的父母隔着长安街遥遥相望，迢迢牵牛星，皎皎河汉女。<br />
<br />
――――――<br />
<br />
<br />
PS 现在是29号1：03，爸爸刚进门。今晚走了估计有10公里。谢谢大家关心。值此盛世，大家晚上尽早回家，不要在长安街上大小便，老实在家看看《新闻联播》，早点洗洗睡了。<br />
<br />
PS2 现在是29号早上10：55。昨晚3点左右，长安街方向传来了“我爱你中国”的歌声，还有方阵前进的伴奏乐，关了窗塞了耳朵还是没法睡。具体情况请大家收看今天各大电台电视台网站小报的头条。<br />
<br />
刚看了大家的留言。不知道到底是多少楼的老爷爷同学出主意说：“打120好。如果你爸爸在街南，上车后说合同医院是协和；若在街北，就说合同医院是同仁。不过值此危机时刻，120大概会绕道兜圈，直取301。”这个点子昨晚已经有人试过，不过想过路的是一个真正的病人。被困在台湾饭店前面路口的时候，我爸看到一辆协和医院的120救护车拉着病人打算过街但被警察拒绝。后来结果怎样不知道，因为那里不让逗留，我爸被一个女警察赶走了，继续在北城流浪。<br />
<br />
PS3 现在是29号早上11：08。爸爸刚才跟我说，他昨晚在大街上流浪的时候想，他这前半生不是不成功，而是很失败，因为房子车子股票什么都没有。其实他记错了，都有的。只不过房子出租了、车子卖掉了、股票套牢了。<br />
