																											<div id="postmessage_338699" class="t_msgfont">记者暗访甘肃“摸吧” 10块钱吧女可随便摸(图) <br />
来源：大河网 2009年11月23日<br />
[提要] 一张桌子几把椅子、数瓶啤酒、“山寨”版的舞台和灯光、以及游走在各个角落、衣着暴露的舞女，任由舞伴的双手在她们身上的任何地方揉捏，压场“好戏”的节目更是不堪入目，这是兰州“摸吧”的真实写照。学生沉湎于此，居民苦不堪言，呼吁政府严管<br />
&nbsp; &nbsp; 夜色之下 “群魔”乱舞<br />
<br />
　　安宁桃海宾馆所在院内暗藏摸吧，记者打入直击“七宗罪”<br />
<br />
　　本报采访组<br />
<br />
　　灯色、酒色、舞女色、色色皆秽，乐声、闹声、酒令声、声声是噪……演绎这种场景的，是兰州安宁区桃海宾馆院内一处打着啤酒城招牌的摸吧。附近的居民对此怨声载道，加之摸吧地处安宁高校区附近，不时有一些学生模样的面孔出现其中。每到午夜，摸吧里作为压场“好戏”的节目更是不堪入目，屡屡接到读者举报后，西部商报记者对此处进行了暗访，直击这家摸吧的“七宗罪”。<br />
<br />
　　“罪宗”一：特殊的消费群体<br />
<br />
　　只需花10元钱买3瓶啤酒，就可以取得进场“资格”；一张桌子、几把椅子、数瓶啤酒、“山寨”版的舞台和灯光、以及舞台边缘直立的钢管……最关键的，是游走在各个角落、衣着暴露的舞女，若再掏10元钱，她们就可以陪客人共舞3曲，而且任由舞伴的双手在她们身上的任何地方揉捏，在兰州，这被形象地称之为“摸吧”。日前，西部商报记者根据近期屡屡接到的读者举报的情况，在安宁区桃海宾馆门前一处仅有啤酒城字样的娱乐场所内，见到了摸吧的“标准化”场景。<br />
<br />
　　在夜色中，这家暗藏于桃海宾馆门前沿街建筑3楼的摸吧并不起眼，而楼上传出的劲爆乐声和挂在窗户上的彩灯则打破了夜的沉寂；若不是宾馆停车场保安指引，普通人很难发现摸吧设在宾馆院内漆黑一角的大门。从大门进入，沿楼梯而上，四周的墙壁上布满了涂鸦式的女体曲线，颇有令人意乱神迷的味道；混合着嘈杂音乐由远及近，登至3楼，掀开污浊的门帘，一派熙攘杂乱的场景便展现在记者的面前。<br />
<br />
　　“罪宗”二：游走挑逗言语调情<br />
<br />
　　“帅哥，来，请你跳个舞。”刚一落座，一位身着烟灰色低领T恤、浑身散发着浓烈香水味的舞女便将一只胳膊搭在了记者的肩上，另一只手拉着记者的手臂扭捏着，装作很熟悉的样子，眼神投向不远处一个漆黑的用木板隔出的角落，嗲声嗲气的准备拉着记者走入那个不时人进人出的“舞池”。遭到记者推脱后，舞女故作嗔怪的一甩手臂悻悻离去，转而走向邻桌，瞅准一位身穿大红色缎面夹袄、胡子拉碴的打工族，很随意的坐在他的腿上，使出各种风骚手段和挑逗言语，终于软磨硬缠的成功邀其共舞，二人携手消失在昏暗的角落中。<br />
<br />
　　约莫10分钟过后，烟灰T恤女子与红夹袄打工族走出了“舞池”。而此时，舞女脸上已难见了那种暧昧的眼神，表情木讷的走回先前那张桌子，紧跟其后的“红夹袄”似乎意犹未尽，坐下后还牵起舞女的手准备继续拉她坐在腿上，舞女一番扭捏之后，半推半就的坐了下去，却伸出手在“红夹袄”耳鬓私语了几句，待“红夹袄”从其怀中将揉成一团的钞票中拿出两张5元面值的人民币拍在其手中时，舞女方才又现她迷离的眼神，挑起嘴唇在那副满是胡茬的脸上亲吻一下，然后头也没回的转向了另一桌客人。<br />
<br />
　　“罪宗”三：以赌为名促销<br />
<br />
　　比起摸吧里容纳的数十张酒桌，舞女毕竟是少数。此时，“舞曲”显得沉闷，而生财有道的老板在摸吧正中的圆形舞台上摆起了“擂台”，黄色塑料啤酒桌边围满七八个配套方凳，摸吧工作人员扮演“庄家”角色，手持无线麦克向在场的所有人发起邀请：“各位好朋友们，10元就可以一试运气，‘沙子’大‘拖拉机’小……”。<br />
<br />
　　“庄家”所述的“沙子”和“拖拉机”，实际是扑克类赌博游戏的一种，又叫炸金花：这种游戏以玩家手中的三张牌比输赢，花色同为黑桃、红桃、梅花、或方片的牌为大，花色不同的顺子（三张连牌）为小。而与传统赌博以金钱做筹码论输赢不同的是，这场变相的赌博游戏赢家只有两个――持牌的玩家赢得的是或啤酒、或饮料、或香烟的实物，发牌的“庄家”促销的是每10元钱筹码对应的远比市场价高出许多的听装啤酒、饮料和香烟等商品。<br />
<br />
　　不长时间，在“庄家”极具诱惑力的怂恿之下，不少酒客奔上台去，爽快的掏出另一个消费性质不同的10元钱，参与到这场变相的赌博游戏中来，二三十个厮杀与博弈下来，“庄家”的怀中已坐拥近千元现金。<br />
<br />
　　“罪宗”四：酒客丑态毕现<br />
<br />
　　就在舞台中央实为促销的变相赌博游戏火热进行之间，摸吧里的舞女仍游走于各个酒桌之前。环顾四周，乌烟瘴气的环境中坐着各色人等，有戴着金丝边眼睛、西装革履的“小资”，有其貌不扬却又衣着得体的工薪阶层，也有举止粗狂不修边幅的打工一族，更有稚气未脱囊中羞涩的学生。他们的桌子上清一色的码着瓶装啤酒，有喝闷酒的、也有觥筹交错的、还有猜拳行酒令的，个个喝得面红耳赤，大有不醉不归之欲。<br />
<br />
　　期间，一名身穿蓝色夹克衫的青年男子似乎已不胜酒力，一会跳上舞台对正在进行的赌博游戏指指点点，一会又回到座位上翘起双腿摇晃着椅子。不久，只听“嗵”的一声，寻着发出声响的地方找去，却看见那名男子捂着屁股挣扎着从地上爬了起来，旁边急忙跑去的服务生拎起折了后面两条腿的凳子，扔到了墙角。此时，台上的“庄家”半开玩笑半认真的责骂着摇坏椅子的客人，声称若再坐坏就要赔偿。却不想10分钟没到，再次传来熟悉的闷响――又一把椅子被同样的客人摇折了双腿。“庄家”终于压抑不住“舍财”的怒火，拿着无线麦克冲下台去，怒斥一顿之后，叫服务生换来一把更为结实的椅子，一场“危机”方才作罢。<br />
<br />
　　罪宗五：“脱”出压场节目<br />
<br />
　　时间终于进入午夜零时，舞女和酒客们一夜劳顿之后都略显疲态，舞台中央的“擂台”似乎也缺乏了诱惑的力量；而大厅内的喧哗依旧，难道这里真的“不醉不归”？恰在记者准备离去之时，舞台灯光突然全部熄灭，伴随着“精彩马上开始”的话音，摸吧里忽然一阵骚动，几乎同时，一名身穿黑色透明薄纱睡裙、隐约露出黑色内衣的妙龄女郎登上舞台，伴随着场下雷动的掌声和刺耳的呼哨声，时而在舞台周边做着各种不堪入目的挑逗动作，时而有疾步走向舞台边缘直立的钢管摩挲着、并攀上钢管顶端倒挂在半空中让裙摆落下。此时，场面一片混乱，酒客们似乎都似乎都失去了理智，争相拥挤着凑到舞台边上，喊声、尖叫声、呼哨声与杂乱的掌声连成一片，直至曲终。<br />
<br />
　　短暂“冷场”之间，妙龄女郎刚刚长吁一口气，躁动的音乐再次响起，场下再度沸腾。只见被称之DANCER的女郎用双手拇指勾起肩上的衣带，几番摇摆之后便露出先前略显朦胧的内衣内裤。之后妙龄女郎再度重复着同样的动作，不断操控着场下酒客们冲动的情绪，直至第三曲音乐末了，妙龄女郎脱去身上仅剩的两件内衣，引发全场躁动。<br />
<br />
　　“罪宗”六：混乱的治安状况<br />
<br />
　　记者注意到，在“精彩”进行之时，摸吧的工作人员已悄然锁闭了这里惟一的进出口。一扇防盗门将摸吧与外界完全隔离，纵使记者试图以“有事要走”的借口向工作人员陈情请求离去，但却换来“这会不行，等10分钟以后再说”的答复；而就在此时，摸吧工作人员对门外的叩门声更是充耳不闻。<br />
<br />
　　许是因为场上妙龄女郎的舞姿太具诱惑力，大厅一角的客人不知何因发生了口角。两桌人剑拔弩张之间，守候在舞台周围观察客人是否有违“不许接打电话、不许拍照录像”的“游戏规则”行为的工作人员，纷纷奔上前去，将两桌客人拉开，制止了一场殴斗。而虽然其中一方客人要求立场，但仍得到了摸吧工作人员的“婉言”劝说。<br />
<br />
　　待到曲终人散之时，紧闭的大门终于被留守的工作人员打开。“精彩”不再的摸吧里大部门人如鸟兽散，而意犹未尽的酒客在的士高音乐响起之时争相恐后的跨上舞台尽情疯癫。此时，记者留意到，这家摸吧根本没有醒目的消防安全通道标识，一旦火情发生，或许根本不曾存在的“生命之路”将吞噬。<br />
<br />
　　“罪宗”七：周边的备受滋扰<br />
<br />
　　虽然已是次日凌晨，但这家摸吧依旧霓虹闪烁、歌舞升平。尽管记者已经出门离去，但摸吧传来的劲爆音乐还是响彻桃海宾馆方圆500米之内的整个夜空。而一条马路之隔的地方，恰是长风厂居民小区，沿街的居民对这种“夜半歌声”苦不堪言。一位杨姓住户在接受记者采访时就大倒“苦水”：“根本睡不着觉，每天晚上都要到两三点钟。家里还有上初三的孩子，写作业时无法专心不说，孩子第二天上学时的精神状态能保证吗？”<br />
<br />
　　与杨女士同样怨声载道的还有长风厂居民小区的其他住户，一位李姓住户言辞激烈的说：“听说前一段时间警察把这里查了，怎么又开了？”而安宁某高校的蔡老师亦对此表示不解：“安宁区是兰州市的高校区，公安部门确实应该加大力度对摸吧的查处，不然学生都沉浸于此，荒废学业的同时，也会对学生的心理造成伤害，更会将它们父母含辛茹苦提供的生活费耗在这种污秽的环境中。”来源：西部商报</div>

							
							
							
							
