关于妥善做好应对日全食工作的通知 <br />
国办发明电[2009]14号 <br />
<br />
&nbsp; &nbsp; 各省、自治区、直辖市人民政府，国务院有关部门： <br />
<br />
&nbsp; &nbsp; 据国际天文联合会和紫金山天文台预测，7月22日将发生日全食现象。本次日全食持续时间长、涉及区域广，从日食初亏到复原长达两个小时，其中全食持续时间最长可达6分钟左右，全食带覆盖印度中部、缅甸以及我国长江流域等广大地区，偏食带则覆盖我国全境。这次日全食为科学研究、科学普及和观测观赏带来难得机遇。同时，由于日全食期间能见度下降、气温降低、湿度上升，会对交通运输、生产作业、通信安全、社会治安等带来一定影响，也可能在部分人群中产生迷信猜测和心理恐慌。为妥善做好应对工作，经国务院同意，现就有关事项通知如下： <br />
<br />
&nbsp; &nbsp; 一、加强科普宣传 <br />
<br />
&nbsp; &nbsp; 各级科技部门和科协组织要充分利用此次日全食为科普宣传提供的良好机会，组织相关专家通过电视、广播、网络、报纸等多种渠道，广泛开展天文知识科普宣传活动，对日全食现象进行科学解释说明，对相关防范知识进行宣传普及。宣传工作要深入农村、山区、林区等偏远地区，消除部分群众的迷信和恐惧心理。同时，要注重对科学观测方式方法的宣传教育，避免造成观测人员视觉损伤。请中央和地方各级宣传部门和媒体给予大力支持。 <br />
<br />
&nbsp; &nbsp; 二、做好相关预案和应对准备 <br />
<br />
&nbsp; &nbsp; 针对日全食发生期间因天空亮度骤暗带来的不利影响和问题，请有关部门按照职责分工，制订完善相关预案，积极做好应对准备工作。交通运输、铁路、民航、通信、电力等部门和单位要及早做好安排部署，确保安全、畅通；市政设施管理部门要做好充分准备，日全食发生时及时开启城市道路照明设备；医疗卫生部门要注意防范光线不足对医疗救治的不利影响；气象部门要加强天气监测，及时做好可能出现的灾害性天气预警信息发布；城市高空作业、建筑施工等作业单位要提前通知施工人员，并制订严密的安全保障措施，防止发生安全事故；质检、工商部门要对生产、销售假冒伪劣观测器材进行依法查处；公安部门要做好日全食可能引发的社会治安等问题的防范应对工作，加强公路特别是高速公路交通应急管理，防止发生重特大交通事故。 <br />
<br />
&nbsp; &nbsp; 三、加强旅游和观测的组织与管理 <br />
<br />
&nbsp; &nbsp; 一些地方已将这次日全食观测作为旅游活动进行组织，有关地区和旅游部门要针对可能出现的大量观测人员聚集情况，提前做出安排部署，切实做好游客数量、交通住宿和天文观测设施使用等预警信息发布工作，引导游客合理选择观测地点。要加强对观测活动的组织管理，制订合理的人群疏散等应急预案，妥善做好安全保卫工作，严防发生拥堵、踩踏等事件。四、强化值守应急 <br />
<br />
&nbsp; &nbsp; 各地区、各有关部门要密切关注此次日全食事件，切实加强值守应急，及时掌握有关情况，<strong>一旦发生突发事件，要立即启动相关应急预案，迅速组织力量，妥善高效处置</strong>，最大程度降低和减轻日全食造成的不利影响。重大情况要按照有关规定及时向国务院报告。 <br />
