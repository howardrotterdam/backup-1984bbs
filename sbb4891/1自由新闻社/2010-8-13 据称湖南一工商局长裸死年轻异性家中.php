<br />
<br />
有消息称，黎圣喜死时身体赤裸，异性女子年龄20余岁，这些说法未获警方证实。永定公安分局局长王祥军透露，事发地在一个小区，而非酒店。<br />
<br />
陈美林告诉记者，黎圣喜是正处级干部，归湖南省工商局管理，黎死后家人和上级单位未同意尸检，目前已下葬，张家界市分管领导有到场送花圈。<br />
<br />
“此事影响很坏。”陈美林表示，12日，张家界市委专门召开常委会，市委书记在会上要求加强全市干部作风管理，严肃纪律。<br />
<br />
