													<h2>胡锦涛考察腾讯公司获赠QQ号</h2>
						
						
																											<div id="postmessage_818656" class="t_msgfont">晶报讯 昨天下午，在莲花山公园内的特区纪念园里，时不时有市民在胡锦涛总书记手植树前合影留念。一位正在拍照的市民王先生告诉记者，大家对这棵总书记亲手种下的桂花树很关注，希望相关部门能好好管护。<br />
<br />
视频：胡锦涛考察腾讯公司 勉励青年员工<br />
树种不一般：<br />
观赏价值高，象征果实飘香<br />
总书记植的树，究竟是一棵什么样的桂花树？昨天莲花山公园管理处副主任邵志芳向记者揭秘：总书记种下的是金桂，其为木犀科常绿灌木或小乔木，终年枝叶繁茂，花朵金黄，秀丽而不娇，花香馥郁，幽香而不露，在众多桂花中观赏价值最高。<br />
据介绍，桂花是我国十大传统名贵花卉之一，距今已经有2500多年的栽培历史，也是珠三角地区的常见树种。由于桂花可以赏花，又能闻香，现代都市园林造景时，经常会选用上它。桂花还具有丰富的文化意义。“物之美者，招摇之桂”，桂花一直是世上美好、高雅事物的象征。“八月桂花遍地开，桂花开放幸福来”，历代民间也皆视桂花为吉祥之兆。而举凡中榜登科，仕途得志也被称为“折桂”，对获得殊荣者则被誉为拥有“桂冠”。邵志芳表示，特区纪念园里种植金桂，也象征着深圳经济特区30年建设的胜利果实满园飘香。<br />
选点有讲究：<br />
与30棵大榕树互成对景<br />
“现在，特区纪念园中已经种植了30棵大榕树，构成了环形的纪念林，寓意特区建立30周年。把金桂树种植在纪念园入口处，既能与纪念园的30棵榕树区别开来，又能和这些榕树形成很好的对景效果。”公园管理处副主任邵志芳介绍说。<br />
所谓“对景”，就是“我把你作为景，你也把我作为景”，这种景在园林中有很多，游客在榕树和浮雕墙后远望可见茂冠的金桂，而站在金桂树后，又可见远山下半圆形的榕树林，这种呼应的效果很有层次，耐人寻味。<br />
护树有方案：<br />
专人养护精心栽培<br />
昨天下午，记者在胡总书记手植树旁看到，莲花山公园管理处已安排专人进行看护，树下半径两米左右设置临时护栏，工人正在平整树根部分的沙土层，并铺上草皮。<br />
据公园管理处技术人员介绍，这棵金桂树是在佛山仔细挑选出来的，后运到深圳，其树龄为30年，长势良好，树冠茂盛。由于金桂树喜光，喜温暖湿润和凉爽气候，栽培地全日照和半日照均适应，但以半日照生长更为旺盛，颇耐寒，不耐干旱。同时，金桂又喜肥沃、湿润和排水良好的壤土和沙质壤土。针对这些生长特点，管理处将安排专业人员，定期对其进行施肥养护，管养标准会参照仙湖植物园内的小平手植树进行，尽量不进行修剪，维护树木的自然风貌，确保金桂树能茁壮健康成长。<br />
此外，管理部门还表示，今后在这棵金桂树下，将圈定范围设置固定护栏，以保护枝干不受人为损坏。“喜欢观树赏花，不需要和金桂‘零距离’接触！”公园工作人员幽默地说。<br />
考察点回访<br />
胡锦涛总书记深圳考察期间，在深圳的社区、企业、地标性场所都留下了深情的足迹和殷殷的叮嘱。<br />
这几天，与他亲密接触的深圳人，还在回味着这一份备受鼓舞的见面――<br />
大亚湾核电基地<br />
总书记主动和核电人一一握手<br />
“总书记您好！”9月5日上午10时30分，胡锦涛总书记来到岭澳核电站二期3号机组主控室，当值班长冯文彪与其他同事一起，激动不已紧紧地握住了总书记的手。<br />
冯文彪昨晚向记者回忆，当总书记步入主控室，双眼与总书记和蔼的目光相对，双手与总书记温热的手掌相握，他感到一种从未有过的激动，虽时隔数天，但冯文彪提起当时的情景，仍然激动不已。“总书记一路走过来，都很亲切很主动地跟身边人一一握手，这让我们倍感荣幸。”在现场，冯文彪指着厂区周围5公里、10公里放射线监测情况的监控设备，向总书记介绍反应堆的安全情况，总书记满意地点头。<br />
在4号机组主控室，总书记遇到了他的两位清华校友――中国工程院院士徐大懋、郑健超也等在那儿了。在主控室会议室，总书记发表了简短讲话。他表示核能是新兴能源，国家高度重视发展核能。他充分肯定中广核集团在核电发展上取得的巨大经济效益和安全效益，并要求确保核电安全、稳定运行。总书记强调，为国家的建设，核电站一定要掌握扎实的基础，确保核电站的安全。临走前，总书记还与两位院士校友唠家常并合影。晶报记者 林佩玮<br />
腾讯公司<br />
腾讯赠总书记QQ靓号<br />
你上QQ时可能会和胡锦涛总书记同时在线。9月5日下午，胡锦涛总书记一行到腾讯公司参观考察。记者昨天从腾讯公司获悉，在胡总书记考察参观腾讯期间，腾讯首席执行官马化腾代表腾讯员工特别向总书记赠送了QQ号码。<br />
据腾讯有关负责人透露，马化腾首先在腾讯展厅向总书记详细介绍了腾讯的发展历程、业务情况、腾讯人的风貌和腾讯对自主创新的追求等四方面内容。在即时通信展台前，工作人员使用QQ进行演示，用卡通形象在屏幕上打出“祖国好”的标语和灿烂的笑脸，看到这一幕，总书记的脸上露出笑容。<br />
在腾讯研究院和用户体验与设计中心，总书记走进了小Q机器人研发区域、QQ概念版研发区域、用户体验研究区域。在小Q机器人外观及功能设计工程师座位旁，总书记饶有兴致地观看了工程师通过触碰唤醒小Q机器人，用语音遥控机器人从而控制电脑进行软件操控的演示。<br />
考察期间，马化腾代表腾讯员工，向总书记赠送了精心准备的QQ号码。收到这份特殊礼物，总书记非常高兴，他说：“同志们，很高兴来到腾讯，很高兴在这里见到你们，特别是见到这么多年轻人。你们不光年轻，还那么有朝气，富有创造力。中国的互联网产业能不能跻身世界前列，达到世界一流水平，这个要你们来回答。”晶报记者 康岩慧<br />
渔民村社区<br />
总书记问租客住得好不好<br />
9月5日中午，胡锦涛总书记来到曾是全国最早的万元户村的渔民村，与社区居民亲切交谈。昨日，渔民村渔丰物业管理有限公司的总经理吴颂球向记者重温了和总书记在一起的那激动人心的一刻。<br />
吴颂球告诉记者，胡锦涛总书记参观完渔民村的文化长廊后，就来到村里面社区流动人口和出租屋综合管理服务中心。“当时看到总书记，感觉非常和蔼亲切，他关心地问两个在社区居住的租客‘在这里住得好不好’‘社区的服务到不到位’，感觉书记很关心民生问题。”吴颂球说，虽然在管理中心呆的时间不长，但仍能感觉总书记非常重视这块。<br />
总书记考察渔民村，整个渔民村社区居民每每谈到这个事情，都兴奋不已。渔丰股份有限公司常务副总经理黄兴炎仍显得异常兴奋，他说，总书记在观看渔民村图片展后勉励大家继续改革创新，将改革事业往前推进，在科学发展观上有新的突破。<br />
当天，总书记还走到社区文化广场，和许多居民交流握手。“到现在整个社区居民还都在谈论，大家都感到很兴奋和开心。”吴颂球说。（记者 郑毅/文 赖犁/图 林佩玮 康岩慧 卢育鸿）</div>

							
							
							
							
