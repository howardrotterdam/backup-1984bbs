新华网深圳9月7日电（记者 秦杰）莲花山下，深圳湾畔，三角梅装点着生机勃勃的城市，红灯笼衬托出喜庆热烈的气氛。在深圳经济特区建立30周年之际，中共中央总书记、国家主席、中央军委主席胡锦涛来到广东深圳考察工作。<br />
<br />
<br />
1'7''视频：胡锦涛考察腾讯公司 勉励青年员工<br />
9月4日至6日，胡锦涛总书记在中共中央政治局委员、广东省委书记汪洋陪同下，深入深圳企业、科研机构、居民社区，就加快转变经济发展方式、推动经济社会又好又快发展进行调查研究，勉励广大干部群众再接再厉、奋发进取，在新起点上创造新业绩、谱写新篇章。<br />
<br />
“加快转变经济发展方式，自主创新是根本，培养人才是关键”<br />
<br />
提高自主创新能力，推动产业结构升级，使经济特区在加快转变经济发展方式中充分发挥带头作用，是胡锦涛关注的一个问题。在深圳期间，总书记重点考察了这方面工作。<br />
<br />
近年来，深圳涌现出一批充满活力的创新型企业，腾讯计算机系统有限公司就是其中的佼佼者。胡锦涛来到腾讯公司，实地了解这家企业在互联网领域进行自主研发的情况。在网络安全监控室、腾讯研究院，总书记饶有兴致地观看QQ最新概念版、眼动仪实验演示。得知这家企业1．2万多名员工平均年龄只有26．8岁、85％以上拥有本科以上学历，胡锦涛语重心长地对企业年轻员工说，国家的未来在创新，创新的希望在青年。你们公司年轻人多，充满了蓬勃朝气和创新活力，令人感到振奋。希望大家充分发挥聪明才智，坚持自主创新，推动企业不断发展，为壮大民族互联网产业贡献力量。<br />
<br />
为全面了解深圳建立科技创新体系、培育战略性新兴产业的情况，胡锦涛在中国科学院深圳先进技术研究院考察时，观看了深圳市一些科技企业和先进技术研究院的科研成果展示。在企业展区，华为、中兴、研祥、大族激光、北科生物、比亚迪等企业的创新成果琳琅满目；在先进技术研究院展区，智能机器人、全民低成本健康与高端医学影像、超级计算机与数字城市感知等研发成果精彩纷呈，集中展示出深圳在电子通信、电动汽车、生物医药、新能源等方面的发展前景。总书记看得仔细、问得深入。他表示，看了这些展示，真切感受到深圳经济特区的创造活力，也看到了建设创新型国家的希望所在。<br />
<br />
在同先进技术研究院科研人员交谈时，胡锦涛说，加快转变经济发展方式，自主创新是根本，培养人才是关键。他希望研究院的同志们立志做自主创新的“领头羊”，围绕经济社会发展需要，突破更多关键核心技术，为产业转型升级提供有力的科技支撑。<br />
