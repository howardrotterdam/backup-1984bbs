																											<div id="postmessage_132928" class="t_msgfont">浅析太孙题词<br />
<br />
<br />
<br />
全国政协委员、军事科学院战争理论和战略研究部副部长、博士、研究员、大校、太孙，昨晚在强国论坛又题词了。<br />
<br />
题词为“祝愿强国论坛更好地为人民服务大力弘扬毛泽东思想<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;祝广大网友身体健康工作顺利”<br />
<br />
请语文老师分析一下：“祝愿”后边能跟“为人民服务”、“大力弘扬”吗？<br />
<br />
俺觉得，祝愿这个词，后边总是要接“被祝愿者将来的某种行为的结果或者状态”，比如健康、愉快、顺风、成功、进步等等，而不能祝愿人家实行某种行为，比如，只能说祝你学习进步、工作顺利，不能只说祝你学习、祝你工作。太孙的题词，如果简化一下就是“祝你服务”、祝你“弘扬”。俺觉得，如果你非要“祝愿”人家的行为，可以用“希望”这个词。比如，希望你服务，希望你弘扬。这个，大概小学生都懂的吧！<br />
<br />
另，“大力弘扬毛泽东思想”似乎放在“更好地为人民服务”前面更好些。因为，弘扬是手段，服务是目的。太孙题词如此混乱，说明他的思想本来就是混乱的。<br />
<br />
最后一句“祝广大网友身体健康工作顺利”，我一看见“广大”就别扭。不如干脆说“网友们”。“广大网友”这个词组，是说人多，它不是全称，再广大，也不是全部。那么，“祝广大网友身体健康工作顺利”，就是说，不是全部网友，而只是“广大网友”，总有一些，我是不祝愿的。而“网友们”就是一个全称，用“网友们”就没有这个毛病了。<br />
<br />
这些，往小了说，也许是俺才疏学浅，不理解上意，往大了说，是人家太孙极大地丰富和发展了祖国的语言文化。学习了。<br />
至于太孙字的艺术性，俺就不多说了，仅用16个字概括：前无古人，后无来者，集萃中华，烛照千秋。<br />
