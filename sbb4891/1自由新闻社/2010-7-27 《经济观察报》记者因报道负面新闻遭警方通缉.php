													<h2>凯恩股份电池业务前景不明巨额关联交易价值几何</h2>
						
						
																											<div id="postmessage_761482" class="t_msgfont"><a href="http://www.eeo.com.cn/eeo/jjgcb/2010/07/19/175732.shtml" target="_blank">http://www.eeo.com.cn/eeo/jjgcb/2010/07/19/175732.shtml</a><br />
<br />
仇子明&nbsp;&nbsp;14:13&nbsp;&nbsp;2010-07-17<br />
　　7月20日，凯恩股份（002012.SZ）将发布半年报。在此前的一季报中，凯恩股份预测上半年净利润将同比增长200%，凯恩股份实际控制人王白浪先生6月24日在央视的一档节目中也重申了这个业绩预测。<br />
<br />
其实，上半年净利润增长200%的预期背后，凯恩股份能否实现依然存疑，在网上的股吧里，有投资者戏称，对于股票代码为2012的凯恩股份而言，半年报能否兑现，是一场“2012”式的考验。<br />
<br />
凯恩股份董事长王白浪在央视节目中指出，凯恩股份未来业绩的增长点，主要在于上市公司新收购的大股东资产――凯恩电池。但据本报了解，这个凯恩电池之前经过了曲折的“变脸”。<br />
<br />
收购凯恩电池始末<br />
<br />
在完成收购凯恩电池股权这笔关联交易完成前，凯恩股份一直是一家造纸企业，主打产品为电解电容器纸，在收购凯恩电池后，公司进入电池领域。<br />
<br />
2009年5月15日，凯恩股份（002012.SZ）发布公告称拟做强电池新业务，第一步以自有资金及银行借款9648.86万元收购大股东凯恩集团及在集团工作关联人吴雄鹰所持有的凯恩电池累计78.8%的股权；第二步通过定向增发再融资2.5亿元，做大电池业务。<br />
<br />
凯恩股份称“电池业务将成为公司未来利润的重要增长点，增强公司的整体盈利能力”。2009年6月26日，凯恩股份完成对凯恩电池的收购。<br />
<br />
为此凯恩股份付出的代价差不多是，上市后的2005-2009年5年净利润（1亿元左右）的总和。<br />
<br />
但是，2010年1月11日，凯恩股份叫停了定向增发，理由却是“受宏观经济波动及产业政策的影响，完成收购和非公开发行后，凯恩电池对公司的影响也存在不确定性”。<br />
<br />
从巨资收购凯恩电池，到叫停定向增发，对电池产业的看法改变是在半年时间内。<br />
<br />
然而6月24日，王白浪在接受采访时却又再次对凯恩电池前景看好。在他说这话之前，却又把自己持有的凯恩股份股票大幅度进行了减持。<br />
<br />
“钱”景不明的电池业务<br />
<br />
其实在收购之前，凯恩电池并没有骄人的盈利记录。<br />
<br />
浙江天健东方会计师事务所出具的评估报告显示，在2009年1-3月，凯恩电池仅产生净利39.26万元，经营活动产生的现金流量净额为 -1569.40万元，凯恩电池甚至还将其包括土地使用权、房屋建筑物、机械设备、镍氢电池项目在建工程在内的几乎全部资产抵押给了建设银行遂昌县支行，担保借款3500万元。<br />
<br />
尽管凯恩电池此前盈利能力不强，但凭着造纸主业，凯恩股份的盈利能力却一直在稳中提升。凯恩股份在2008年扭亏为盈，2009年，公司将净利润提升至6104.42万元。<br />
<br />
在完成收购之后，当年凯恩电池没有给上市公司带来惊喜。2009年年报显示，公司的电池业务毛利率仅为24.99%，远低于其原主业造纸业的36.63%，且公司营业收入还同比下滑5.56%。<br />
<br />
年报同时还显示，公司的特种食品包装品毛利率为44.63%；而工业配套用纸的毛利率则为39.52%，营业收入同比增长46.35%。<br />
<br />
其实，凯恩电池式的“变脸”，凯恩集团并非是第一次。<br />
<br />
早在2006年，上市不到三年的凯恩集团就筹划将上市公司卖掉。<br />
<br />
2007年2月，凯恩股份发布公告称，因大股东凯恩集团的公司股权大部分已经质押，存在过户障碍，因此卖壳给凡尔顿公司的计划搁浅。<br />
<br />
有意思的是，在终止卖壳计划的公告中，凯恩集团宣称：“作为凯恩股份的控股股东，凯恩集团对凯恩股份的发展充满信心。”<br />
<br />
令人费解的是，既然看好上市公司的发展，大股东又何必在上市三年不到的时间内，就急于卖壳呢？</div>

							
							
							
							
