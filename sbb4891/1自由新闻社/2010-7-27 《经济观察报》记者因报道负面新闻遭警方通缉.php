													<h2>凯恩股份偷天换日谜团</h2>
						
						
																											<div id="postmessage_761492" class="t_msgfont"><a href="http://finance.ifeng.com/stock/fxdp/20100605/2279543.shtml" target="_blank">http://finance.ifeng.com/stock/fxdp/20100605/2279543.shtml</a><br />
<br />
2010年06月05日 00:32经济观察报 仇子明<br />
<br />
5月24日，50岁的李子华在浙江丽水市医院拿到了诊断书，被打坏的牙齿治疗费需要1.2万元。<br />
<br />
这位凯恩股份[13.39 -1.54%](002012.SZ)的前员工，声称三年前自己被公司“报复性地解聘”，并开始上访。去年7月15日，在上访后回家的路上，被人严重打伤，虽然遂昌县人民法院已经判处施暴人有期徒刑，但是李子华依然认为这是公司方面的人对他上访的报复。<br />
<br />
李子华也因此继续搜集证据并举报凯恩股份大股东凯恩集团的实际控制人王白浪。本报记者实地调查发现，在这件事情的背后，有太多关于王白浪和凯恩股份的传言和疑团。从诸多证据来看，当年凯恩集团改制时，政府文件被有意篡改，致使国有资产被严重贱卖。而上市公司的资产也有被侵吞的嫌疑。<br />
<br />
据本报从浙江证监局了解到，证监局和有关政府部门已经开始对凯恩股份和其大股东凯恩集团涉及到的改制问题、土地转让问题以及上市公司资产转让方面的问题进行调查。<br />
<br />
王白浪大幅减持股票<br />
<br />
截至2009年12月31日，凯恩集团持有凯恩股份6705.06万股、王白浪持有662.50万股，分别以34.42%和3.40%的持股比例分居第一、二大股东。因王白浪又累计持有凯恩集团46.66%的股权，为上市公司的实际控制人。<br />
<br />
2009年中期，凯恩股份宣称，拟受让凯恩集团旗下的凯恩电池78.8%的股权。收购完成后拟定向增发4000万股，募资2.5亿元以做大做强电池业务。然而今年1月9日，凯恩股份叫停了增发募资。1月27日，凯恩集团和王白浪拉开了大规模的凯恩股份股票减持序幕。当日，通过大宗交易减持了250万股，减持价平均为10.19元。此后，凯恩集团又通过大宗交易减持了16笔。<br />
<br />
3月5日，凯恩股份公告称，凯恩集团仍有进一步减持的计划。截至年3月31日，凯恩集团累计减持2590万股，套现金额近2.7亿元(除仍在质押的 3500万股外，凯恩集团几乎全部清仓)。而王白浪本人持有的股票完全退出，套现6750.86万元。通过上述减持，王白浪套现金额近2亿元。<br />
<br />
其实凯恩股份财务报表显示，其业绩表现良好，处于持续增长通道。2009年公司实现净利润超6100万元，同比增长146.21%；2010年一季度实现净利润2452万元，同比增长621.45%。<br />
<br />
为何上市公司业绩趋好，王白浪却“疯狂减持”，上市公司在公告中的解释为：出于大股东自身投资资金的需要。<br />
<br />
本报一直试图联系王白浪本人，但他的手机号已被告知为空号。奇怪的是，王白浪也很久没有出现在公司了。凯恩股份董秘田智强和凯恩股份门卫都告诉记者，最近他们很少看见王白浪。<br />
<br />
5月26日中午，本报记者在遂昌县上南门路找到了王白浪的住所。但其住所铁皮大门紧锁，远眺可见铁门内有两三幢小洋楼，记者敲门，一直无人应答。在大门外的信箱里塞满了报纸和信件，说明信箱已久未开启。门外停着一辆红色的上海大众POLO车，知情人士指认，车主为王白浪之妻。<br />
<br />
当日晚上，记者再次来到王白浪的住所，亦未见有人。附近的一家台球室女老板告诉本报，她已经有两个月没见到王白浪了。<br />
<br />
采访过程中，甚至有人称王白浪不知去向。凯恩股份董秘田智强回应说，自己不便评论，“王白浪是公司的实际控制人，如果他出了什么问题，上市公司会发公告的。”<br />
<br />
不过，本报多方调查得知，王白浪本人近期并无出境记录。<br />
<br />
偷天换日的改制<br />
<br />
2001年，浙江遂昌凯恩集团有限公司这家遂昌县属国有独资企业进行了改制，这家总资产1.36亿元，净资产709万元，所属土地评估值1317万元的公司，最终被以89万元的价格转让给了王白浪、邱忠瑞、张程伟等16位自然人。他们基本都是这家公司的高层管理人员。<br />
<br />
在2001年4月2日，遂昌县国有资产管理局和这16为自然人签订的 《资产转让协议》中，按照凯恩集团提留、剥离后净资产(595.757万元)85%的优惠，作价89万元。这份转让协议声称参照的是遂昌县委【1998】25号、遂政【1999】24号文件。<br />
<br />
本报查阅了“县委【1998】25号”《关于进一步推进国有、城镇集体企业股份合作制改造的若干意见》这一文件，其第6页的原文表述为：“对整体购买且购买时一次性付清价款的，经收取部门批准，可按购买数额的大小给予15%左右的价格优惠。”这里的意思很明确，应该是要付购买数额的85%。<br />
<br />
另一份“遂政【1999】24号”《关于改制企业土地使用权处置的意见》这一文件的第四条原文表述是：“对改制企业经资产核销、剥离后，按85%优惠”。这一表述正确的理解应该是原价格上打了八五折，与“按85%的优惠”可以说一字之差，谬之千里。<br />
<br />
这家2004年就顺利上市的企业，在这次改制的过程中，竟然是按照净资产提留、剥离后净资产作为底价，而且还被玩了这种巧妙的文字游戏，可以说是达到了偷天换日的目的。凯恩集团当年的转让价格，即使按照净资产转让，优惠后也应该高达506万元。<br />
<br />
后来在凯恩股份招股说明书第45页在介绍发起人股东凯恩集团的基本情况时，亦以双引号声称原文引用上述两文件原文：“优惠85%”。这是明显的造假，这种表述在上述两个政府文件里根本没有。但这种改后的表述，与当时的《资产转让协议》却能吻合了。<br />
<br />
也就是说，根据政府文件，王白浪等在受让国有资产时，“按85%优惠”的表述，即优惠额度为八五折(按85%优惠)，并非是后来的一五折(优惠85%)。<br />
<br />
在这次改制过程中，这样的手段造成国有资产流失只是一方面。另一方面，有一块土地还没有被计入转让资产中。<br />
<br />
本报获得的遂地价(2001)第1号和遂地价(2002)第2号的土地估价报告显示：遂地价 (2001)第1号是在县城中心的环城南路13号的一宗土地，面积计44.39亩，为改制而评估的价值是771.9257万元；遂地价(2001)第2号是原一车间、三车间等的生产车间、仓库、办公楼、草料场等17宗土地，计135.94亩，为改制而评估的价值是1317.18万元。<br />
<br />
不过，在凯恩集团改制时的《资产转让协议》中，这块44.39亩土地并没有被列入其中。王白浪等人只支付了遂地价(2002)第2号土地评估报告中的17宗土地出让金，这44.39亩分文未付。<br />
<br />
知情人士告诉记者，位于遂昌县环城南路13号地44.39亩现在的土地证就在县国土局。<br />
<br />
一位曾与王白浪有过商业合作的杭州某企业高层对此表示：“王白浪玩弄‘15%’和‘85%’的文字，使得凯恩股份涉嫌在招股说明书中篡改政府文件，构成欺诈上市”。<br />
<br />
记者还了解到，上述18宗土地的土地使用证中，出让年限是“50年”。但是，“遂政【1999】24号”文件第三条中原文规定：“(土地)出让年限为30年”。知情人士认为“国家损失了20年的土地出让金”。<br />
<br />
记者在遂昌县国土局求证时，该局纪委书记季春光、分管土地转让的副局长徐晓平、土地利用科的科长王彩方、办公室的徐姓主任对此均没有提供任何解释和说明。<br />
<br />
凯恩集团改制的经手人、原遂昌县经贸局的副局长林勤，在本报记者上门采访时，被其拒绝。<br />
<br />
收购款迷踪<br />
<br />
凯恩股份上市前大股东的改制疑团重重。上市后上市公司的资产重组和数笔资金往来也让人难以理解。故事要从凯丰纸业公司讲起。<br />
<br />
设立于2003年8月23日的凯丰纸业，注册资本1500万元，凯恩股份、张陆根、计皓(凯恩股份现任董事长)分别出资630万元、270万元、600万元。<br />
<br />
2004年6月1日，即凯恩股份上市前一个月，张陆根、计皓分别出资840万元、560万元增资凯丰纸业。增资后，张陆根持有凯丰纸业的持股比例达38.28%。凯恩股份并未参与本次增资。<br />
<br />
凯恩股份于2004年7月5日在深交所上市，当年9月便以1100万元人民币收购自然人张陆根所持有的凯丰纸业股权。上市前不增资，上市后收购，这里面的玄机让人无法明白。<br />
<br />
2005年凯恩股份年报显示，凯丰纸业当年亏损865.76万元。2007年，凯恩股份一度试图将凯丰纸业的大部分股权转让给一家芬兰公司。<br />
<br />
知情人士告诉本报，凯恩股份收购凯丰纸业股权中，涉嫌侵吞上市公司资产。而接下来一系列围绕收购的资金流转更是让人眼花缭乱。<br />
<br />
据举报人向记者出示的材料中称，2004年9月16日，凯恩股份从中国银行[3.55 -0.28%]丽水遂昌县支行的账户上，把1100万元付至张陆根个人账户。但2004年9月22日和27日，凯恩股份主管财务工作的现任董事顾飞鹰却从张陆根账户上分别取出310万元和800万元存入其个人账户上。而据了解，顾飞鹰是王白浪的亲戚。<br />
<br />
2004年9月27日顾飞鹰从张陆根账户上取出的这800万元存入自己账户的当天，这笔钱又被汇入其丈夫陈?的账户上。<br />
<br />
2005年4月4日，陈?又将这笔钱中的795万元，通过其在建设银行[4.91 0.20%]天水支行的个人账户汇入王静波在建设银行杭州城西支行的账户。王静波为王白浪的胞弟。<br />
<br />
知情人士告诉记者，王静波用这笔资金，于2005年4月在杭州设立一家名为杭州凯恩投资有限公司(下称“杭州凯恩”)，并通过该公司将上述资金又回笼至王白浪所掌控的凯恩集团。<br />
<br />
“当时，王白浪找到我们老板，要我们出1000万，他出1000万，共同设立杭州凯恩。”杭州凯恩财务人士告诉记者。2005年4月12日，这家公司成立，实际操作者就是其弟弟王静波。“我们没想到的是，公司才成立13天，王白浪就以急需用钱为名，抽走了1000万元的注册资金。”<br />
<br />
据杭州凯恩有关人士介绍，王白浪方面的1000万元资本金中，795万元来自王静波，205万元来自王白浪的遂昌运丰投资公司 (下称“遂昌运丰”)。<br />
<br />
本报了解到，从杭州凯恩出来的这几笔钱流向是，4月25日，500万元打给了凯恩集团，500万元汇给凯恩集团的全资子公司杭州康德。随后，杭州康德又将其中的205万元汇给遂昌运丰，2006年8月15日，剩下的295万元又汇给凯恩集团。至此，王静波名义下的795万元又回到了凯恩股份第一大股东凯恩集团的手中。以“借款”的名义抽调的杭州凯恩1000万元注册资金，至今未归还。<br />
<br />
杭州凯恩的一位人士还告诉本报，“我们通过一些手段查证过王静波在注册杭州凯恩的资金来源，他的资金就是来自凯恩股份动用上市公司1100万元现金购买张陆根资产的这笔钱。所有的证据，我们都交给了杭州市上城区公安分局。”<br />
<br />
对于上述举报情形，凯恩股份现任党委书记张程伟表示，他本人有所听闻，但强调“那些都是胡说”。<br />
<br />
6月4日，本报从浙江证监局获悉，该局已介入对凯恩股份的调查。</div>

							
							
							
							
