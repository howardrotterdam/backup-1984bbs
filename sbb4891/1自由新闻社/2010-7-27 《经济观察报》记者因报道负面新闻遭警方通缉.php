													<h2>08年的旧事 目前还在料理善后</h2>
						
						
																											<div id="postmessage_762198" class="t_msgfont">银泰绑架了谁？<br />
《法制日报》所属《法人》杂志<br />
2008年11月03日11:52　　来源： 我有话说　查看评论(0) 好文我顶(0) 　　<br />
《法制日报》所属《法人》杂志执行主编阮加文 <br />
<br />
<br />
直到半年以前，位于浙江丽水的银泰公司还只是中国地产界一个微不足道的角色。伴随着这家企业的崩溃，当数万个家庭因之而陷入贫困和绝望的时候，人们才如梦初醒――把一个名不见经传的公司同浙江南部这个城市的命运连在一起。<br />
<br />
　　“银泰房地产的崩盘在丽水并不是个案，许多靠民间集资起家的房地产公司都会陆续倒掉。”据一位十分了解丽水房地产市场的专家说，集资开发房地产的现象在四年前的丽水就已非常普遍。特别是在中央多项调控房地产市场的政策措施出台以后，原来依赖银行贷款、习惯空手套白狼的房地产公司，一下子失去了银行这根拐杖，就开始在民间资金这一块大动脑筋，有的是以借款的名义，有的是以入股的名义，有的干脆就是集资，广泛吸纳民间资本。<br />
<br />
　　但是，最有吸引力的手段只有一个，那就是房地产公司承诺给予数倍于银行存款利息的回报。据这位专家的调查，丽水市区42家房地产公司有一大半实施了“集资开发”计划，其中涉及的各类“集资人”数以万计。<br />
<br />
　　这位专家同时表示，最近五年来，丽水的房地产市场一直显得不太正常。丽水作为浙江省的欠发达地区，一度被称为“浙江的西藏”。直到今年，丽水市的人均GDP也才一万多元人民币，但其楼市涨幅却连年居高不下，看上去一枝独秀的房地产业与丽水整个经济增长水平完全格格不入。即使是按照官方统计数据，2005年丽水市区的楼市均价也达到了每平方米4392元，比上年度增长了百分之二十以上，而实际楼价比官方公开的数据要高得多。到2007年，丽水市的许多楼盘甚至卖到了每平米1万多元。<br />
<br />
　　“丽水的楼价这些年涨得太快了。”一位在两家房地产公司都放有资金的小企业主告诉记者，仅看商品住宅的涨势就明白，房地产公司要想集资真是太容易。2004年每平方米也就两三千元，到半年前已经是八千上万元了，谁都相信向房地产公司集资不会有什么风险。<br />
<br />
　　直到银泰崩溃之前，丽水电视台数年来从未中断的一档节目就是房产节目。在黄金时段里，丽水市大大小小的房地产公司老板粉墨登场，春风得意地在电视里大谈他的公司如何成为“大众公司”，大小股东就有数百个，并且都获得了怎样令人羡慕的回报。这些老板不惜巨资投入电视广告，目的不言而喻，希望获得更多的“投资”――让观众把钱存进他们公司以获取高额利息。耳濡目染的丽水观众当然不可能无动于衷，整个丽水市的民间资本正以这种合法或者非法的形式源源不断地流向在此前炙手可热的房地产公司。<br />
<br />
　　“丽水房地产公司大肆集资，政府难辞其咎。那些官员哪个不知道电视上每晚播出的那些东西。”银泰公司的一位集资户仍然无法掩饰自己的愤怒。<br />
<br />
　　丽水市建设局房地产处毛处长在接受记者电话采访时说，他不知道丽水市有没有集资开发房地产的情况，也不知道房地产业对整个GDP的贡献率是多少，知道了也不会说，这个问题太敏感。<br />
<br />
　　浙江省政府的一位要求匿名的官员说，由于许多地方政府的财政收入过分依赖房地产业，对房地产业企业的各种违规操作往往是睁一只眼闭一只眼，有些地方的政府部门甚至与房地产企业达成某种默契，与中央政府搏弈。更有甚者，有的官员还直接参与房地产集资，牟取高利，一有风吹草动，他们凭着手中的权力可以迅速全身而退，不会有任何风险。<br />
<br />
　　据记者调查，官方的统计数据表明，2005年，丽水市区的地税收入是49304万元，其中房地产业15936万元，占32.3％。而得到丽水市多家房地产企业老板认同的一个说法是：房地产业对丽水财政收入的贡献率高达50％以上。<br />
<br />
　　国务院发展研究中心的陈同教授告诉记者，非法集资开发房地产现象的蔓延，不仅使中央政府宏观调控房地产市场的各项政策措施的功能失灵，也可能引发严重的社会后果――楼市泡沫一旦破灭，或者房地产企业经营不善而破产，无法无天的房地产老板携款逃跑，成千上万的集资户将血本无归。</div>

							
							
							
							
