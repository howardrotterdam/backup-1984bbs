<br />
2010-06-19 14:33 来源：经济观察报 作者：仇子明<br />
<br />
<i>而在2000年12月凯恩集团改制前向遂昌县国资管理局提交的在职员工花名册上，张陆根此前也一直都是凯恩集团的职工。当年的8月28日，凯恩股份、张陆根、计皓 (凯恩股份现任董事长)分别出资630万元、270万元、600万元设立凯丰纸业。</i><br />
<br />
<img src="http://image.xinmin.cn/2010/06/19/20100619143316735855.gif" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　凯恩股份(000012.SZ)澄清公告并没能解释其身上的种种谜团。<br />
<br />
　　6月7日，本报刊发《凯恩股份“偷天换日”谜团》一文后，凯恩股份发布澄清公告称，收购凯丰纸业“履行了有关法律法规规定的程序”，并称遂昌县政府未对凯恩集团改制有“任何异议”。<br />
<br />
　　本报经过进一步调查得知，涉及到的凯丰纸业的收购，以及另外两宗对浙江亨宝德纸业的收购行为中，均可能存在关联关系的嫌疑。<br />
<br />
　　另据本报了解，6月9日-11日，浙江证监局稽查处副处长黄涛一行前去遂昌县对该凯恩股份展开初步调查，调查重点便是上市公司的信息披露。不过该局一位人士告诉本报，“此次并非正式的立案稽查。”<br />
<br />
　　关联人张陆根？<br />
<br />
　　2004年的9月10日，也就是凯恩股份上市两个月之后，出资1110万元收购了“自然人张陆根”所持有的凯丰纸业38.28%股权，公告声称“自然人张陆根与本公司无关联关系，本次收购不构成关联交易”。<br />
<br />
　　但是一位凯恩集团离职高管却告诉本报，当时这笔收购发生时，张陆根还是凯恩集团办公室副主任。<br />
<br />
　　而在2000年12月凯恩集团改制前向遂昌县国资管理局提交的在职员工花名册上，张陆根此前也一直都是凯恩集团的职工。<br />
<br />
　　本报记者在这份花名册中看到，序号为99号的在职员工张陆根，出生于1968年7月，于1990年8月参加工作，凯恩改制时工龄为10.4年。凯恩股份一位退休的员工透露，2004年左右，张陆根的年薪也“最多不会超过5万元”。张陆根当时的大致薪资水平，记者在遂昌县一位离职的前政府官员处亦得到证实。<br />
<br />
　　谜团不止于此。这笔收购的背后还存在着涉嫌通过土地转让存在非法利益输送。<br />
<br />
　　杭州市上城区公安分局的一位人士透露，在他们手上涉及到凯恩集团举报材料中，2003年8月1日，尚未上市的凯恩股份和龙游县国土局签订协议，斥资800万元拿下了235亩土地的使用权，土地使用权证为龙游国用 (2004)字第2114号，地号：211-16-5-0。<br />
<br />
　　当年的8月28日，凯恩股份、张陆根、计皓 (凯恩股份现任董事长)分别出资630万元、270万元、600万元设立凯丰纸业。<br />
<br />
　　这位人士证实，3天后的2003年9月1日，上述235亩土地的使用权由凯恩股份变更到凯丰纸业手中。<br />
<br />
　　2004年6月1日，张陆根、计皓以一宗评估价值为2347.68万元的土地使用权向凯丰纸业增资，其中1400万元作为资本投入(张陆根840万元、计皓560万元)，407.709万元作为资本公积，剩余部分计539.971万元作为应付该两名自然人股东款项。增资后，张陆根的出资比例为 38.28%。这38.28%的股权又于3个月后以1100万元卖给了刚刚上市的凯恩股份。<br />
<br />
　　一位知情人士告诉本报，据其看到的龙游宏宇会计师事务所出具的评估报告 (龙会评估【2004】030号)，张陆根和计皓名下的用于上述增资的一块235亩工业用地的土地使用权评估价为2347.68万元。这块土地正是凯恩股份2003年8月以800万元购得，并于当年9月1日变更为凯丰纸业的那235亩工业用地。<br />
<br />
　　原属于凯恩股份的这块土地使用权被变更到刚成立3天的凯丰纸业手中，之后又在凯丰纸业增资时“离奇”地变成了张陆根和计皓所有。而且其在一年不到的时间内，评估值就涨了三倍变成2347.68万元。<br />
<br />
　　这位知情人士透露，他曾专程前往龙游县查过了当时的土地使用权证，发现上面的“土地使用权人被涂改过了”。据其描述：“在龙游宏宇会计师事务所的验资报告、评估报告(龙会师验【2004】062号)中，土地使用权人上被贴纸覆盖，贴纸上写上了张陆根和计皓的名字。”<br />
<br />
　　上述杭州市上城区公安分局的人士亦告诉本报，2004年，上述土地使用权证上的使用权人的确被涂改为张陆根和计皓。他表示，关于上述土地使用权的相关证据在该局有存档。<br />
<br />
　　如果这些属实，将明显违反了《中华人民共和国土地管理法》。本报还从浙江省国土资源厅一位官员处咨询得知，“本属于企业的土地使用权，如果转让、变更给个人，一定要经过国家收储，以及招、拍、挂的程序。”从2003年8月1日至2004年6月1日，龙游县的这块土地使用权在不到一年的时间内，由公司变更为自然人，按照土地使用权变更的程序，是不可能的。<br />
<br />
　　关联交易“非关联化”<br />
<br />
　　事实上，在凯恩股份还有其他的并购行为，存在这样的信息披露疑点。<br />
<br />
　　2004年9月，凯恩股份同时还斥资1000万元收购了自然人林浩所持有的浙江亨宝德纸业50%的股权；2005年12月，再度斥资655万元收购自然人吴赛娥所持有的浙江亨宝德纸业30%的股权。这两位自然人，当时的凯恩股份公告均称“与公司无关联关系”。<br />
<br />
　　但记者在上述凯恩集团改制时提交的花名册中，找到了编号为151的吴赛娥的名字，其出生于1963年5月，于1981年9月参加工作；而林浩在该花名册中的编号为707，生于1974年10月，于1992年12月参加工作。<br />
<br />
　　而事实上，上述凯恩集团离职高管告诉本报，上述收购发生时，林浩和吴赛娥均为凯恩集团财务人员，吴赛娥甚至至今仍在凯恩集团财务部门工作。他表示，“亨宝德纸业最初注册资本为2000万元，林浩、吴赛娥只是普通员工，其薪资水平不足以分别持有50%和30%的股权。”他认为这两个人和张陆根一样，只是代持者。<br />
<br />
　　该人士还告诉本报，凯恩股份上市募集的资金，投资兴建了生产线、办公楼，建设施工方为凯恩建筑工程有限公司，这是凯恩房地产开发有限公司的全资子公司，而后者又是凯恩集团的全资子公司。这笔募集资金使用涉嫌关联交易，但也没被披露出来。<br />
<br />
　　北京一位资深的证券律师对此分析认为，“若情况属实，这便是典型的关联交易‘非关联化’，大股东和上市公司通过欺骗股东的方式，严重损害了中小股东的利益，中小股东可以通过法律途径进行维权，向上市公司索赔。若在香港，上市公司这样的利益输送行为一定是严惩不贷的。”<br />
<br />
　　对于2000年底之前凯恩集团改制提交给遂昌县国资管理局的这份员工花名册，上述凯恩集团前高管也表示有问题。<br />
<br />
　　该花名册显示凯恩集团当时共有员工1264人，花名册每一页均有遂昌县劳动人事局盖章，该局审批时间为2001年1月10日。根据这份名单，凯恩集团提留了1431.11万元的员工改制补偿金。<br />
<br />
　　本报在凯恩集团于2000年、2001年、2002年这三年向遂昌县劳动人事局、统计局提供的 《劳动情况》表中却看到，凯恩集团上述三年的员工总数分别为789人、790人和800人，三份表上均有凯恩股份董事长王白浪章印。这两组人数显然存在矛盾。<br />
<br />
　　这位人士却称，1998年凯恩集团发起设立时已将绝大部分员工划入了凯恩股份，2001改制后，在凯恩集团供职的员工仅有30人左右。“1999年至2001年期间，凯恩集团的工资表可以做证。”但是本报至发稿时并没有看到这份工资表。</div>

							
							
							
							
