																											<div id="postmessage_189039" class="t_msgfont">国务院新闻办公室近日下发通知，要求内地新闻网站实施实名注册登陆制度，网民需对自己在网络上发表的言论承担社会责任。<br />
<br />
据南京市官员透露，国新办7月27日下发的这份通知要求内地各新闻网站取消现行的新闻稿件匿名跟帖或发帖功能，转而实施实名注册登陆制度，即网民在新闻网站注册时必须填写真实姓名、身份证号码等信息，通过验证后方可登陆跟帖或发帖，对新闻事件发表言论。网民在跟帖或发帖上传信息时，各新闻网站必须提示网民需经审核方可发表，而网民亦需对自己在网络上发表的言论承担社会责任。同时，各新闻网站不能再针对新闻事件设置网民投票或民意调查等。<br />
<br />
通知表示，上述新规将纳入新闻网站年度资质审核考评标淮，不过目前只适用于新闻网站，网络论坛则暂时不受此限制，且新规的具体实施时间与细节尚未明确。<br />
<br />
该官员表示，目前中国对网络媒体的管理远远落后于网络媒体的发展，互联网已经成为一些群体组织策划活动的工具，而政府的信任度则出现高度危机。因此加强网络监管、实行网络实名制势在必行。网络实名制国际上已有先例可循，例如今年6月韩国信息通信部规定该国网站实行网络实名制，网民发布信息必须先接受身份验证。<br />
<br />
杭州市人大常委会今年上半年曾审议通过《杭州市计算机信息网络安全保护管理条例》，提出网民发帖、写博或玩网游都需要提供有效身份证明。由于遭受强烈质疑，原定于今年5月1日开始实施的这一法规至今仍束之高阁。<br />
