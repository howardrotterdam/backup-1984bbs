Lorant's testimony provides independent corroboration for an anonymous source who first came forward on February 12 to reveal to two New York researchers (Clay and Shawn Pickering) that the UN meeting had taken place.Lorant的话与另一个匿名消息来源不谋而合，该消息于12日向两名纽约的研究人员Clay and Shawn Pickering透露了联合国密会的相关情况。The source's testimony was revealed in two articles published by this author on February 13 and 20 , and a follow up article by Robert Morningstar on Feb 25.该消息的具体内容已发表在2月13日和20日的两篇文章中，25日的article by Robert Morningsta对此也有涉及。 Lorant's testimony gives more detailed information of what happened at the meeting, and is consistent with statements from the original source of the UN meeting concerning approximately 40 participants, multiple issues being discussed as well as the UFO phenomenon, and public reactions to increased UFO sightings. Most importantly, Lorant provides insight into reasons for a significant change in official public policy towards the UFO phenomenon. Lorant的话更提供了本次会议的更多细节，并且与会议的消息源头信息保持一致。本次会议有大约40人参加，讨论话题涉及UFO现象、公众对于UFO目击的反应。最重要的是，Lorant得以让我们窥探官方对于UFO态度转变的一些原因。 <br />
<br />
The reported results of the UN meetings indicate agreement was reached for an international strategy to more seriously study the UFO phenomenon in a manner that maintains present levels of social stability.据称，会议达成协议，在保持社会稳定的前提下采取国际层面上更为严肃仔细的UFO研究态度。This raises the possibility that those presenting information on UFOs/extraterrestrial life deemed to threaten national stability or financial systems, would receive special attention by national security agencies.这提高了一种可能性，那就是现有的关于UFO和外星人的被视为国家安全和金融稳定威胁的信息将受到国家安全机构更好的“照顾”。 The meetings results suggest that an official change in government policy from 'debunking' to 'openness' concerning UFO's would lead to official approval to narrowly define study of the UFO phenomenon. However, those claiming to have first hand experience of extraterrestrial life would be targeted as charlatans.会议结果显示了官方态度从“拆穿虚假”到“公开”的转变，这会导致官方对UFO研究进行一个狭义的界定，从而，一些据称掌握一手资料的个人可能被视为江湖骗子。 The UN meeting thereby suggested continuation of policies aimed at exploiting divisions in the UFO community between researchers advocating a strictly scientific study of the UFO phenomenon, and those wishing to discuss testimonies/documentation concerning extraterrestrial life visiting Earth. 联合国会议建议继续贯彻那些旨在利用研究派系分歧的政策，这些派系有的主张对UFO进行科学意义上严格的研究，有些主张讨论那些关于外星人的证词和档案。 <br />
