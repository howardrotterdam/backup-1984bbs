A French aviation expert has come forward to reveal his participation in a secret meeting held at the United Nations New York headquarters that discussed the UFO phenomenon from February 12-14. Gilles Lorant is an attaché to the Centre National de la Recherche Scientifique (CNRS- National Center for Scientific Research) which is a government-funded research organization, under the administrative authority of France's Ministry of Research. He is also a member Institute of Advanced Studies for National Defense (IHEDN - the French Organization that produced the 1999 COMETA Report). Both CNRS and IHEDN are prestigious scientific organizations in France, and Lorant was invited to attend the UN meetings in a professional capacity. A report released by a French UFO organization (Federation of European Airplane ), of which Lorant is an executive member, contained a summary of the UN meeting where he describes what happened at the meeting. 一名法国航空砖家最近透露了他参与其中的2008年2月12-14日于纽约联合国总部秘密举行的关于UFO的会议。 Gilles Lorant 是法国研究部下属国家科研中心的一名专员，也是国防高级研究所的成员。这两家机构都是法国资深的科研部门。Lorant本人以砖家身份被邀请参加此次联合国会议。由Lorant任管理人员的一家法国UFO机构（欧洲航空联盟）发表的一份报告提供了本次会议的概述，在报告中他介绍了会议内容。 <br />
