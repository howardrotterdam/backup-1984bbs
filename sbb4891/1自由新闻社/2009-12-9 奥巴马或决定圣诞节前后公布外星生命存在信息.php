2 法国砖家确认关于UFO的联合国秘密会议 <br />
这也就是在说有三名美国国会议员参加了本次联合国密会。会议主要涉及UFO相关事项带来的社会动荡问题，这些UFO相关事项主要指不断发生的UFO目击事件、一些据称掌握UFO以及外星人和其他未来全球性事件信息一手资料的个人的证词。有报道说对于UFO目击事件的负面社会反应可以重创经济，甚至导致一些国家陷入衰退。 <br />
<br />
The emphasis on possible societal upheaval due to growing UFO sightings was reminiscent of recommendations of the 1953 Robertson Panel, convened by the CIA, which identified adverse public reactions as a major security threat that could be exploited by foreign enemies. 对于不断发生的幽浮目击事件可能导致社会动荡的强调让人联想起1953年罗伯逊专家组的建议，这个专家小组当时由中情局召集，最终得出结论：对幽浮目击的负面反应――作为一个重大安全威胁――有被国际敌人利用的可能。（关于Robertson Panel，参见维基百科，这个罗伯逊专家组最初就是因为频繁发生于华盛顿特区的UFO事件才被召集<a href="http://en.wikipedia.org/wiki/Robertson_Panel" target="_blank">http://en.wikipedia.org/wiki/Robertson_Panel</a>） <br />
<br />
The UN discussions appeared to match concerns identified in both the 1953 Robertson Panel and the 1961 Brookings Report to the U.S. Congress, over ramifications of UFO reports and the eventual discovery of extraterrestrial life. 联合国本次讨论所关注的似乎跟1953罗伯逊专家组和递交国会的1961布鲁克林报告一脉相承，布鲁克林报告事关幽浮调查的各种结论和E.T.的最终发现问题。 <br />
<br />
2009 was raised as the year when an important change in official attitudes concerning the UFO phenomenon could be made. 2009年被视为重要的一年，这一年将见证官方对于UFO现象的态度的重大转变。This suggested that the official debunking policy underway since the 1953 Robertson Panel would be abandoned and more serious study of the UFO phenomenon would occur. This indicated an important policy shift where the study of UFO's would be officially encouraged.这意味着始于1953年罗伯逊专家组的“拆穿UFO谎言”政策的放弃，转入对UFO现象的严肃研究，这个重大的政策转变将使UFO研究得到官方认可和鼓励。 However, certain global conditions would have to be met for the official opening. Most important appeared to be social stability in G-8 countries and the maintenance of liberal democratic systems in developing nations. Lorant also suggested that an official opening in attitudes would only occur if UFO sightings continued to occur at present levels. 然而，官方的公开态度必须建立在实现一系列条件的基础上，最重要的是G8国家的社会稳定和发展中国家民主体制的保持。Lorant同时指出UFO目击现象持续以现有的频率继续发生也是官方表明态度的必要条件。 <br />
