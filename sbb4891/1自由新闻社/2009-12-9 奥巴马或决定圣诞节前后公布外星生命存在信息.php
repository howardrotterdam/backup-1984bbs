																											<div id="postmessage_363153" class="t_msgfont">美国51区<br />
<br />
　　好莱坞制作的一些惊恐大片，比如《独立日》以及《X档案》等影片，使人们对“51号地区”不再感到那么的陌生，但却为这个地区蒙上了一层神秘的面纱，从这些大片中人们可能会得出一个结论，该地区一定储存着美国政府正在秘密研制的一些绝密技术。会是什么呢？<br />
<br />
　　布什死守“绿屋”，普京无缘一见<br />
<br />
　　51区是美国一个充满神秘色彩的秘密空军基地，位于“新郎山”脚下，离内华达州的拉斯维加斯130公里，“绿屋”被相信就处于这个军事基地。所谓的“绿屋”，里面是国家首脑观看外星人冰冻尸体的地方，每一位新当选的美国总统都会前去参观。<br />
<br />
　　“绿屋”内可能不仅仅包括外星人的残骸，可能还有外星人乘坐的飞船碎片，该飞船是1947年坠毁的。陌生人从来不被允许访问这个基地，即使是军人，也被严格禁止泄漏任何有关51区内部生活的秘密。当俄罗斯总统普京访问美国的时候，布什总统并没有邀请普京访问绿屋，也没有让他看任何外星生物的尸体。<br />
<br />
　　军方否认其存在，军事基地藏“虚幻”<br />
<br />
　　1950年当美国政府在内华达州建立核武器试验地时，51区也在其中。在军事地图上，试验地被分区编号，“51区”因此得名，整个地区的面积为144平方英里，是一块位于内华达州沙漠地带的干涸河床。直到1994年以前，美国军方都对外否认存在这个连军事飞机也不准进入其上空的禁区，现在也不愿透露任何有关它的情况。作为美国土地上保密程度最高的一块地盘，自然会产生种种神秘的传说，51号地区也被人跟外星人挂上了钩。<br />
<br />
　　长期以来，这里一直是UFO爱好者和指控美国政府一直在这一地区进行某种阴谋活动的批评人士希望了解的神秘地方。表面看来，“51号地区”就象是一个由飞机机库、储存仓库以及跑道组成的普通试验场。但进一步的观察却显示出令人很感兴趣的内容。第一个引人注目之处就是该地区南部一条长度超过3英里的跑道，跑道一边是好几处飞机库，其中几个外形非常巨大，屋顶都被漆成白色。第二个值得注意的地方就是一座空中交通管制天线，这座天线底座呈长方形，长400英尺，而天线本身高150英尺，所以在天气晴朗的条件下，从12英里以外都可以看到这座天线。<br />
<br />
　　总而言之，上述两点使得这一空军轰炸机基地看起来与其说是一处典型的军事基地不如说是象科幻电影里的一个虚拟场景。不明飞行物研究专家认为51区的行为与宇宙外的文明有直接关系。<br />
<br />
　　秘密基地仍是谜，不发一言惹猜疑<br />
<br />
　　在这个基地周围可以经常发现一些球形，三角形以及类似飞盘形状的不明飞行物，有相片和一些视频证据可以证明这些观察到的现象。三年前，布什总统命令收回内华达州州政府管理51区的权限，现在的基地归五角大楼和美国政府直接管辖。值得注意的是，10年前民用飞机也被禁止穿越基地上空。<br />
<br />
　　51区吸引世界的注意力已经有30多年了，但是几个世纪以来，美国政府从未发布过任何关于基地以及基地周围发现神秘军事目标的声明。其间，有一些旅行社组织过一些基地外围的短程旅行，相信“事实的真相就在那里”这句话的基地狂热爱好者们每年5月30日到51区附近的地方来一次聚会。热心家们在认为是外星人建在秘密基地外部的375号宇宙高速公路旁边，讨论政府有什么秘密计划。邻近雷切尔和白端两个城镇的居民，在1986年的夏天，都能够感觉到他们脚下地面的震动，当地居民在每个星期四早上7点钟的时候，都能看到一些奇怪的现象。人们也能听到基地那边传来的奇怪声音。但是当人们提出要起诉基地军队的时候，一切就又会消失，恢复成原来的样子。<br />
<br />
　　51区对于整个世界来说仍然是一个谜，即使能从卫星照片中分析出一些数据，每个人的看法不同，也会产生不同的解释。也许，有一天美国政府会公布出这个秘密，让我们拭目以待吧！<br />
