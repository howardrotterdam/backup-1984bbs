Lorant revealed that a series of UN meetings were held over a three day period. In the meetings there was discussion of a confidential report, prepared by the U.S. Air Force and a branch of the National Guard, concerning the societal impact of UFO sightings. Lorant指出，三天内举行了一系列会议，讨论了一份由美国空军和国防部门准备的报告，报告事关UFO目击事件的社会影响问题。 <br />
<br />
The initial meeting was held on February 12 and, according to Lorant's report, led to a request by three U.S. Senators for further discussions. Additional meetings were held immediately after on February 13 and 14. The implication is that three U.S. Senators attended the secret UN meetings. The meetings were very concerned about social upheaval from continued UFO sightings and the testimonies of individuals claiming to have first hand knowledge about UFO's, extraterrestrial life, and future global events. It was reported that adverse public reactions to UFO sightings could have a dramatic economic impact that could lead to recessions in some countries. 据Lorant说，会议始于2月12，在三位美国国会议员的要求下，13和14日又进行了追加会议。&nbsp;&nbsp;<br />
