													<h2>摇滚叛徒的迷思</h2>
						
						
																											<div id="postmessage_263139" class="t_msgfont">2009年09月28日 星期一<br />
 <br />
近日城中有股声音口诛笔伐人称「香港摇滚之父」著名歌手夏韶声，「香港摇滚之父」是以前香港人给夏先生起的，但吊诡是他年介五十多岁，自年少开始努力于摇滚乐，在香港地，摇滚乐是不到钱，开饭基本生计尚可，但不会因「搞摇滚乐」而致富。现实地说，阁下的努力，要名成利就才叫作被肯定，只是「名成」而不「利就」，有个乜乜之父的名堂，对夏韶声来说，脸上是光荣的，但心里有多少苦涩，相信是无数只拍掌而不会买他唱片，甚至「我好 buy 摇滚乐但唔好叫我买飞睇」的香港普遍市民所不明白的。<br />
<br />
当细心聆听过夏韶声早前上香港电台《讲东讲西》节目畅谈他的作品后，不如先简略说他在节目中的内容：他说话明显比以前世故，衬托着他的新作「大国崛起」歌曲，表达他对祖国的尊敬，节目中他也谈了多年来在「香港所谓的摇滚乐坛娱乐圈」的经历，详情大家可有下方连结收听有关节目。<br />
<br />
背叛摇滚与放弃摇滚<br />
<br />
有说法指夏韶声背叛了摇滚乐，他作为香港摇滚之父，现在出席亲中保皇爱国爱港爱和谐民建联搞的国庆庆祝活动为表演嘉宾，于是他实时被定性为「转了呔」、「背叛了摇滚乐」、甚至骂他「亲共音乐走狗」之类说话，听见说他转呔和背叛了摇滚乐的指控，我感到非常奇怪，也想起了以下问题，读者不妨思考一下：<br />
<br />
1. 「夏韶声背叛了摇滚乐」，奇怪是他多年来在摇滚乐中苦拼，有几多个「自称好爱摇滚乐」的「所谓摇滚乐迷」是真金白银不离不弃去捧他的场？<br />
<br />
2. 有乐手说过：「香港只有娱乐圈，没有真正乐坛」，当观众只喜欢看靓仔靓女偶像当宝( 其实夏韶声也很有型，但不及偶像靓仔 ) ，偶像派柴哗哗上台像鬼上身跳跳猴子舞卖广告就财源滚滚，有多少人真心对所谓摇滚乐情有独锺呢？不是没有，但少得不足养活这些乐手，是观众早已放弃了摇滚乐，还是所谓「他背叛了摇滚」呢？？？<br />
<br />
3. 在九十年代，香港出现回归前的经济泡沫，市面上充斥着低俗爆笑烂 Gag 港产片、每天红馆有偶像演唱会、男女个个身光颈靓风花雪月，曾几何时，「玩摇滚乐有咩用呀」这句金石良言，观众是否言犹在耳？直到现在，父母都是「阿仔要学小提琴阿女要学芭蕾」，有几多个怒插夏韶声背叛摇滚乐的人，如果已是为人父母，够唔够胆送你个仔阿女去学电结他？ 请讲得出做得到，够胆至好答，多谢。<br />
<br />
4. 香港的异常消费市场以女性为主导，最赚钱的行业是美容纤体、时装、名牌手袋、化妆品、乜乜物物健体排毒产品，然后就是男女都适用的电子产品，娱乐圈都建基于偶像明星做代言人，成名都是为日后的代言人广告铺路，好奇一问，以夏韶声这类「摇滚乐之父」，有什么产品可以由他做代言人呢？<br />
<br />
5. 一个很简单的问题：请问各位要否开饭？如果不明白问什么，我再清楚地问，请问各位是否需要吃饭的呢？？<br />
<br />
没饭吃才是艺术家&#8231;香港心态<br />
<br />
夏韶声努力多年，香港人早已放弃了摇滚乐，早已看不起摇滚乐人，除了他为音响发烧友制作的靓声 CD 有较佳销量之外，平时有几多人是靠 download 、问人借老翻来 Copy ？(连老翻碟都不买，衰到要老翻再 Copy ) ，夏韶声也好任何摇滚乐人也好，都要吃饭的，现在看见的是，平日见人玩摇滚就嘲讽无前途，见到「摇滚之父」做些到钱的工作，就大喊「你背叛了摇滚乐」，即是要人做圣人，但又不要人吃饭，捱穷才是艺术家，唔到钱的摇滚乐人才是「好人」，香港人会否觉得其实自己的心态也很丑陋？<br />
<br />
夏韶声的音乐造艺非凡，对作曲无比认真，他作品数量虽不及偶像派多，但每一首作品都是费尽心思地去创作，他也有很多经典金曲，如「空凳」、「妈妈我没有做错」，还有「摇摆心窝」、「永不放弃」还有更多，有些歌曲未必上榜，但也非常好听，他是一位真正的音乐人和热爱音乐的歌手，并非妹妹仔上台叫嚣走音扮纯情短裙仔，也不是靓靓仔白脸小弟弟松开几粒衫纽扮型人。香港娱乐界以短视见称，所有娱圈经营者都是以「计数佬」心态去机关算尽，最紧要捧偶像快钱，吹嘘做代言人古古惑惑狂销商品，连皇者级的陈慧琳唱片制作人也说过：「而家 Mac 机好方便，可以俾歌手逐粒音去录，就算疃甲咭簦都可以调效，唱片一定听唔出&#13774;」这样，试问，这种异常的香港娱乐圈生态，又可以吸引多少真正爱音乐和有才华的人才入行呢？<br />
<br />
热血民粹<br />
<br />
夏韶声在出席民建联国庆活动「红色摇滚音乐会」上台演唱新作「大国崛起」时，有观众上台抢咪，并高叫「平反June Fourth」口号，期后坊间有声音骂他背叛了摇滚乐、摇滚叛徒云云，香港人本色，不合己意一于群起痛斥骂之，合心意才是道理，你不认同就是蛇鼠一窝，把自己的意见视为高地，对于有观众上台抢咪叫口号，更是奇上加奇：<br />
<br />
在「民建联国庆活动」上台抢咪喊「平反June Fourth」，但这个是民建联活动，就好像你去日式寿司店投诉要吃中式烧鹅一样令人费解。如果阁下很讨厌民建联，为何又要去该党的活动？当然，不排除有人趁机倒民建联的台，但夏韶声只是表演嘉宾，抢他的咪是否又代表阁下「很爱民主、很公义、很热血」呢？不如像社民连，真的够胆就在尊贵的立法会上向强权扔蕉，此为真正胆色，或当场努斥高官以表民忿，或之前有人在广管局公听会上怒插「 CCTVB」，矛头直指电视台最高层，而不是拿表演嘉宾出气，夏韶声的作品「大国崛起」是他的创作，观众认为不合意大可不理，但人家表演期间上台抢咪叫口号，如果这就是民主热血表现的话，真是教人汗颜。<br />
