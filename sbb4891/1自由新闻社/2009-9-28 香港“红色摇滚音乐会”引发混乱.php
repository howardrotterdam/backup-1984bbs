													<h2>香港“红色摇滚音乐会”引发混乱</h2>
						
						
																											<div id="postmessage_261011" class="t_msgfont">香港摇滚乐之父演出引发国庆大争论<br />
<br />
记者： 黎堡 <br />
香港<br />
Sep 27, 2009<br />
&nbsp;&nbsp;<br />
<br />
在香港，一场国庆音乐会演变成了赞美祖国与谴责北京暴政相较量的舞台，而主要当事人是被誉为“香港摇滚乐之父”的著名歌手夏韶声。<br />
<br />
*有人夺麦克风 主办人谴责*<br />
<br />
这场音乐会星期六在香港热闹的尖沙咀香港文化中心广场举行。被誉为“香港摇滚乐之父”的著名歌手夏韶声在舞台上高唱祖国的歌曲，一些歌迷却在台下唱对台戏，还有人在他正在表演的时候冲上舞台，夺走麦克风。他们高呼“平反June Fourth、结束一党专政”等口号，现场一度相当混乱。<br />
<br />
这场音乐会由香港亲北京的主要政党香港民建联主办，节目的名称就是“红色摇滚音乐会”。民建联中央委员、这场音乐会的统筹策划人黎荣浩对一些人上台夺麦克风、干扰音乐会的举动表示强烈谴责。他对美国之音说：“这些行为，第一，不尊重国家；第二，非常不尊重演出者。对这种行为需要提出谴责。”<br />
<br />
*两首歌曲强烈对比*<br />
<br />
主办者以著名摇滚乐歌手夏韶声作为宣传这场音乐会的主要卖点，并希望把他演唱的这首《大国崛起》作为这次国庆演唱会的压轴戏。歌中唱到，“作为中国人的骄傲，希望大家都来举起你的双手，为我们祖国喝彩，让我们一起唱，跟着我一起唱，祖国，祖国，祖国。”<br />
<br />
然而，在许多香港人的记忆里，夏韶声是一名反对强权、具有正义感的歌手。他20年前为1989年天安门民主运动的学生们唱的一首歌《妈妈我没有过错》曾打动过无数的香港人，并激发了香港民众对北京当局的强烈不满。歌中唱到，“人民狂怒的呼吸，埋葬镇压的声音。妈妈我没有过错，一起继续我与你不死的勇气。”<br />
<br />
香港市民支持爱国民主运动联合会主席司徒华说，当时，夏韶声以这首《妈妈我没有过错》参加过支联会主办的“民主歌声献中华”巡回演出活动，在民众当中引起很大共鸣。他说：“当时这首歌很受欢迎，因为它反映了当时人们对天安们静坐示威的学生的支持和同情。”<br />
<br />
香港立法会民主派议员李卓人说，20年后的今天，许多人依然期待夏韶声能保持当年的勇气感动人们、去影响当权者，但是他们对他现在的表现很失望。他说：“《妈妈我没有过错》与《大国崛起》这两首歌从歌词和题目来看，很明显存在矛盾。就算大国崛起，但没有平反June Fourth，人权没进步，是不是一个大国应有的风范呢？我想，很多人会提出质疑，所以出现了昨天发生的那件事情。”<br />
<br />
*夏韶声称乐意参加红色音乐会*<br />
<br />
夏韶声对自己参与“红色摇滚音乐会”进行了辩护。他说，过去的已经过去，中国已经改变。他还在音乐会前对新闻媒体表示，自己很乐意参与这次音乐会。夏韶声说：“我们很多人都知道，一讲红色，就知道那是中国国旗的颜色。我们中国是红色，而红色摇滚是一个国庆节目，就是我们香港民建联主办的一个摇滚音乐会。以前没有人办过，这是第一次。我很开心可以参与。”<br />
<br />
*司徒华称演艺人改变立场不奇怪*<br />
<br />
香港支联会主席司徒华说，过去20年来夏韶声改变了对中国的看法，他对此并不感到惊讶。司徒华说：“有很多人，因为种种原因改变了自己的立场和看法，这一点并不奇怪。特别是很多歌星、影星，为了自己能在中国大陆演出，或者作品可以在大陆销售，在利益引诱下，他们改变了自己的立场。”<br />
<br />
*演艺人的角色引发争议*<br />
<br />
据报导，在星期六的音乐会上被指责变节、并且在舞台上被人抢去麦克风之后，夏韶声对台下的观众说，他不是领导人，不必发出反对政府的声音。<br />
<br />
对此，立法会议员李卓人表示，夏韶声的确没有责任去指责当局，但他应该承担起一个普通民众所应尽的义务。他说：“每个演艺人士都有自己的自由。他说自己不是领导人，我也同意。他不需要进行政治问责。但是不可否认，我们作为中国人，大家都有一个共同目标，就是希望中国改善民主和自由。如果夏韶声也觉得有这个目标的话，我们希望他朝这个方向走。”<br />
<br />
在夏韶声的个人网站歌迷讨论区，网民们对他的最新演出进行了激烈的争辩。有批评人士说，摇滚乐是反抗者的声音，是他们的呐喊，而不是当权者的工具。支持人士则说，夏韶声是一个歌手，应该尊重他的选择，让他唱出自己的心声。<br />
<br />
民建联中央委员、“红色摇滚音乐会”策划人黎荣浩说，夏韶声享有表达自己由衷心情的自由。他说：“其实，摇滚乐不应该只能悲，不能喜。包括摇滚乐在内的音乐让大家去抒发自己的感受和情绪。如果开心，为什么不能唱出来呢？”<br />
