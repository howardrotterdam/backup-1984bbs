他又敬佩Zhao Ziyang，「渠当年帮学生，好明白学生，我觉得渠系，做好正确慨决定。」 <br />
<br />
Beyond的歌曲向来批判。家强直言，当年演出《长城》时，心情很激动，最深刻那段歌词是「蒙着眼睛，再见往昔景仰的那样一道疤痕，留在地壳头上」，他说：「中国有好悠长慨历史，长城亦都好宏伟，但历史上有好大慨疤痕，为满足某人慨个人欲望，死好多人，呢段歌词我好记得。」 <br />
<br />
<strong>愿流亡民运人士可回国 </strong><br />
<br />
对他来说，June Fourth就是那个永远不能磨灭的疤痕，「呢件事根深蒂固，永远都唔会忘记，亦都唔会原谅，中国政府需要真真正正面对，系咪要等到王丹、吾尔开希客死异乡当作蛹事？呢件事会一代传一代，希望渠O唔好掩耳盗铃。」 <br />
<br />
他相信June Fourth始终会平反，「每年都会钰牛香港年年都有烛光集会，畲蟾龉家，唔但求你一朝一夕改变到，但系20年真系颇长。」他认为现在的领导人应较以前的开明，「要进步，就要认错，但要渠O面对呢件事唔系钊菀住５都至少畀O（流亡民运人士）回国，同埋畀父母拜祭吓亲生骨肉。」 <br />
<br />
不少网民均指纪念Beyond成立20周年的歌曲<strong>《抗战二十年》</strong>非常应景， Beyond曾在2003年北京演唱会表演这首歌，结果字幕被中央电台「恶搞」，「抗战二十年」改为「看这二十年」，「活着必须革命」变了「活着必须努力」(<a href="http://www.hkreporter.com/talks/thread-757309-1-1.html" target="_blank">http://www.hkreporter.com/talks/thread-757309-1-1.html</a>)，家强说此曲不是为June Fourth而写，忽然「应景」，始料不及。 <br />
<br />
<strong>后记：有些人豪情不再 </strong><br />
<br />
巨人要灭声，动动手指头，上天下地，都要平息火气，真相公义天不容问。那年的「民主歌声献中华」，演艺界踊跃参与，今时今日觉得香港太自由很乱的成龙，当年也身在其中，大唱《豪情》；曾任港区人大代表、现任全国政协委员的汪明荃，要以《勇敢的中国人》唤醒中国魂；卢冠廷一曲慷慨激昂的主题曲《为自由》，为虚弱的人心注入力量。 <br />
<br />
背弃了理想，谁人都可以，<strong>当年除了Beyond，卢冠廷、夏韶声、达明一派、谭咏麟也纷纷献曲悼June Fourth。</strong> 20年后，十个救火的少年，走了一个又一个，面对神州这个大金矿，歌手艺人噤若寒蝉，真相淹没在沉默里，冷待采访要求者多，愿意公开分享的歌手寥寥可数。 <br />
<br />
对坚持说真相的人，记者一句感谢脆薄无力，只是20年了，你们没变，歌迷应该很安慰，<font color="red"><strong>对国家爱之深、恨之切的感情，绝非歌颂「大国崛起」可以相提并论。</strong></font><br />
