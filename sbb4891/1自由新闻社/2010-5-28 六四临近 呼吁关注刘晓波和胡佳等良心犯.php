													<h2>中国良心犯妻子们的心声</h2>
						
						
																											<div id="postmessage_645580" class="t_msgfont">这个月是中国最重要的阖家团圆春节假期（中国新年），杜婷访问了中国良心犯的妻子们，包括刘晓波的妻子刘霞、胡佳的妻子曾金燕、谭作人的妻子王庆华、黄琦的妻子曾丽。<br />
<br />
刘晓波是发起零八宪章的知识份子，呼吁政府给予人民更多的自由、人权以及选举，于2009年十二月被判处十一年徒刑。胡佳关注中国民主化、环境运动、爱滋维权，于2008年四月被判处三年六个月。谭作人，以调查2008年四川地震后的豆腐校舍闻名的环境主义者跟写手，于2010年二月被判处五年徒刑。黄琦，网站管理者跟人权行动份子，因创立天网寻人事务所，于2009年十一月被判处三年徒刑。<br />
<br />
下为访问内容的翻译：<br />
<br />
刘霞：「想到11年抚摸不到他，心碎。」<br />
<br />
这是她在二审宣判刘晓波「涉嫌颠覆国家政权」之后在推特上发的讯息。<br />
<br />
判决后几天，杜婷试着跟刘霞联络，她原先答应受访，后来决定把自己的感受写下来：<br />
<br />
“两个人这些年风风雨雨走过来，那么多的事情一 时很难用语言表达清楚，与其有一句没一句地说，还不如我自己零敲碎打地写出来。我这个人嘴挺笨的，一向不善于口头表达，这也不是我的风格，还是文字比较适合我。”<br />
<br />
曾金燕：「只恨吃饭的人太少，期待有一天能做真正的团圆饭吧。」<br />
<br />
她在说这句话时正在为除夕准备传统全家团圆饭。曾金燕2006年与胡佳结婚，2007年起胡佳就被拘留，并于2008年四月被判处三年六个月徒刑，罪名是「煽动颠覆国家政权」。这已经是曾金燕第三个没有丈夫在身边的春节了。<br />
<br />
照顾他们两岁大的女儿是她的生活重心。胡佳在婴儿出生后一个月被扣留了。尽管是那么的痛苦跟伤悲，金燕试着控制情绪，替女儿打造安全温暖的家庭：<br />
<br />
“我把我和胡佳的合影贴在床头，告诉宝宝这是爸爸，去探望胡佳的时候也都尽量带她去，现在她已经知道怎么去监狱，坐什么车了。每次见到胡佳宝宝都很开心，又唱又跳的，把平时我和她做的游戏做给胡佳看。”<br />
<br />
当她年轻时，金燕曾患有严重的心肌炎，使她无法参加高考考试。但为了读大学金燕做了充足的准备，她用药物掩盖了疾病的表征，最终通过体检，进入中国 人民大学读经济学。从此之后，她亲身了解了一个人面对疾病跟死亡时的微小。于是一进大学金燕便开始在一个关注爱滋病问题的民间机构做志愿者，并在那认识了 胡佳。彼时胡佳已经长期从事环保和关注爱滋病患的工作，在当局眼中已是「政治敏感人物」。国保就找金燕谈话，让她不要和胡佳谈恋爱，不过：<br />
<br />
“也许是因为身体的缘故所以我看得比较开吧。人总要经历各种苦，有些人终其一生都无法遇到自己爱的人，我很幸运遇到胡佳。虽然选择和他在一起就注定不会有稳定的生活，但这是我必须承受的。”<br />
<br />
金燕表示当2005跟2006年胡佳老是失踪时非常难熬，到2008年判决出炉，她感到生气跟伤心，但起码确定他在哪里。而2011年的六月26就是他回家的日子：<br />
<br />
“最糟的时候已经过去了，胡佳现在比较平静，我的焦虑感也就随之降低了。但我也知道他回来之后的生活会是一个巨大的挑战。之 前胡佳 每次失踪回来后的头几天对我和他来讲都非常痛苦，他潜意识中会把对抗的情绪带回家，把我当做反抗的对象，虽然他并不想如此。阿兰牧师曾说过『对于一个囚犯 而言，真正的监狱是在他走出监狱的那一天才开始的。”<br />
<br />
王庆华：「我是他的战友，我们早就在一条船上了。」<br />
<br />
在春节前几天，王庆华的先生谭作人被判处五年徒刑。<br />
<br />
“已经是第2个春节不在一起过了，去年的时候他去灾区，其实那个时候更担心他，怕他出事。现在他在看守所，至少人是安全的。”<br />
<br />
王庆华认识谭作人的时候，他还在医院做麻醉师。<br />
<br />
“他当时在华西医科大学附属医院，是成都很好的一家医院。后来离开是因为六四。六四时他去了北京，回来后知道肯定没办法再在 医院工 作下去，就辞职了。一个朋友一直游说他回来做环保，那朋友知道谭作人一直对环保方面有兴趣，再加上那时他父亲身体不好，我们想在老人身边照顾起来方便，就 回来了。”<br />
<br />
他在深圳作过几年生意，一个朋友说服他加入环境保护工作，这也是他的兴趣。谭作人于是回到成都，建立了民间环保社团「绿色江河」，踏遍了成都的山山水水，看到有问题的地方他就写文章，给政府提建议，近些年谭作人一直没有稳定收入，家中的收入来源主要依靠王庆华：<br />
<br />
“他总和朋友们说对不起我，我就很生气。我和他讲『这个家也是我的家，我不觉得一定要男人来养家。我们分工不同，你做的事情我做不了，那我来养家好了。”<br />
<br />
纵然觉得自己做的事不会有危险，但在被抓之前谭作人还是有些预感：<br />
<br />
“国保经常找他谈话，软的硬的，那时候他就感觉到可能要出事。他和我谈过几次，当时他就说如果做这些事都能被抓那就让他们抓 吧。所 以那时开始我们就常和两个女儿讲，爸爸做的事情是对的，但可能会有危险，让她们也有个心理准备。我是没什么想不开的，我一直是个挺洒脱的人。之前他正常做 事的时候我是他的妻子，我支持他。他一旦有了危险，那我就是他的战友，我们早就在一条船上了。”<br />
<br />
之前我不太关心他做的那些事，我只是和他一样是个见到不公平的事就要站出来说话的人。现在他被抓，那我就要代他去参加这些活动，然后，等他出来。<br />
<br />
曾丽：「我只是做了我认为正确的事情，当时我觉得那是对的，现在我依然这么认为。」<br />
<br />
曾丽说她早已习惯过年过节时丈夫不在身边。黄琦在2000年因涉嫌「煽动颠覆国家政权罪」被抓，3年之后宣判罪名成立，被判处有期徒刑5年，2005年黄琦获释。2008年黄琦再次被抓，这次的罪名是「非法持有国家机密文件罪」，被判3年。<br />
<br />
“之前黄琦做生意，我在机关工作，那时候我什么心都不操，一点压力都没有。这些年就完全不一样了，所有的事情都要一个人来承担。05年黄琦出来的时候说『你怎么完全变成了另外一个人』，他希望我还是之前那个无忧无虑，什么事都依赖他的小女人。”<br />
<br />
1998年，曾丽跟黄琦用他们经商得来的财富建立「天网寻人事务所」，帮父母寻找失踪的小孩。<br />
<br />
“有政府支持，有媒体宣传，一切都挺顺利。后来在做的过程中渐渐接触到许多上访者，许多弱势群体，就想尽量帮帮他们，于是我们99年就做了网站，希望能提供一个平台。”<br />
<br />
除了寻人的内容外，还有些是评论跟法轮功以及热比娅（新疆商人及异见人士），就是这些让他们惹上麻烦。<br />
<br />
“最初完全没有想到做这个会有风险，只是觉得顶多是往里贴钱，偶尔也会因为经济上的问题和黄琦抱怨。但那种不快很快就会被看 到父母 找到失踪儿童后一家人团聚的 喜悦所取代，那种幸福感和满足感很难用语言去表达，就是你实实在在帮到了别人，别人从中受益，这是一种很难得的体验。”<br />
<br />
黄琦于2005年被释放，重新将网站开张，转型为人权网站。<br />
<br />
“他更坚定了，也没有了之前的顾虑，还能怎么样呢？大不了再进去吧。但这一次他就不让我参与了，和之前不同这次他知道风险性，再说总要有个人打工挣钱，孩子要读书，大人要吃饭。”<br />
<br />
在黄琦第二次被逮捕后，曾丽就辞掉在北京的工作，专心照顾她父母跟孩子。<br />
<br />
“难是难，但也挺坦然的，我只是做了我认为正确的事情，当时我觉得那是对的，现在我依然这么认为。”<br />
<br />
阅读这些妻子/母亲，自由/抵抗，等待/希望的故事，兰小欢引用普立兹奖得主美国作家Annie Dillard说过的话，有什么可以帮他们持续下去：<br />
<br />
“为了某些比个人存在更大的目的奉献（捐献、掏空）你的生命，以及能让你最快乐的事：为了神、为了减轻他人的痛苦、为了贡献知识、为了文学创作、或是其他等等。快乐唯此可得，远胜空虚迷欢。”</div>

							
							
							
							
