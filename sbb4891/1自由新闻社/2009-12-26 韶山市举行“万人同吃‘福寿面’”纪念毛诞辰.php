													<h2>韶山市举行“万人同吃‘福寿面’”纪念毛诞辰</h2>
						
						
																											<div id="postmessage_397126" class="t_msgfont">韶山举行“三万”活动 隆重纪念毛泽东诞辰116周年<br />
<br />
湘潭在线12月26日讯（记者 欧阳天 报道员 左媛媛 谭胜 李霓）12月 26日，伟大领袖毛泽东诞辰116周年纪念日，韶山红旗招展，万众欢腾，数万游客涌入韶山冲，向毛主席“拜寿”。这天，韶山市举行“万人同吃‘福寿面’”、“向毛泽东同志铜像敬献花篮，万人同唱东方红”和“万人长跑赛”等传统活动，以朴素的方式，隆重纪念主席诞辰，深切缅怀这位世纪伟人的丰功伟绩，在缅怀与感恩中坚定梦想，祝福未来。 <br />
<br />
　　万人同唱东方红 <br />
<br />
　　冬日的清晨，尽管寒气逼人，但韶山冲里却暖意融融。一大早，人们从四面八方赶来，怀着无比崇敬的心情，步行沿着瞻仰大道，来到毛主席铜像前，参加“向毛泽东同志铜像敬献花篮&#161;万人同唱东方红”活动。 <br />
<br />
　　毛泽东同志铜像在青松、鲜花、红旗的映衬下，显得十分庄严肃穆。上午8时，活动正式开始。毛主席亲属代表、湘潭市、湖南省韶山管理局、韶山市领导依次向毛主席铜像敬献花篮，并向主席铜像三鞠躬。青山依旧，故园情深，毛主席的丰功伟绩像不朽的丰碑耸立在人民的心中，耸立在共和国的大地上。此时此刻，现场的人们以及韶山的山山水水、一草一木，都沉浸在对毛泽东同志丰功伟绩和光辉人生的深切缅怀之中…… <br />
<br />
　　“毛主席，人民世世代代怀念您！”《东方红》的音乐声响起，现场万余人都随着旋律放声高唱那首在心中回荡了千百遍的颂歌：“东方红，太阳升，中国出了个毛泽东……”歌声萦绕在韶山冲的上空，唱出了人们对主席的无限敬仰和怀念，唱出了人们建设美好韶山的心愿，唱出了人们奋发向前、开创未来的壮志豪情！歌声在每一个人的心中激荡起阵阵波澜，在韶山的山山水水间久久回荡，人们深情注视着昂然屹立的主席铜像，久久不愿离去…… <br />
<br />
　　据了解，随着中央“一号工程”、省“五个示范工程”和全国人民共建工程在韶山的实施，韶山作为全国爱国主义教育示范基地的功能得到大幅提升，越来越多的人来韶山接受爱国主义和革命传统教育，韶山的红色旅游一路攀升，经济社会实现了跨越发展。今年来韶山参观瞻仰的海内外游客达到450万人次，较去年同期增长25%。“向毛主席铜像敬献花篮”已经成为来韶游客缅怀毛主席的一种特殊的方式。今年11月30日，中共中央政治局常委李长春同志来韶山视察“一号工程”建设情况时，就指出“瞻仰毛主席铜像、向主席铜像敬献花篮等仪式，本身就是对个人灵魂的一种洗礼，是开展爱国主义教育的一种有效形式”。 中共韶山市委书记杨广向记者介绍：“越来越多的来韶游客喜欢用这种文明、朴素而热烈的方式，来缅怀伟人，坚定梦想，祝福未来。” <br />
<br />
　　万人同吃福寿面 <br />
　　<br />
　　12月26日一大清早，天刚蒙蒙亮，韶山村村部大楼前坪就摆满了八仙桌，韶山村村民和来自全国各地的游客都赶来这里，兴致勃勃地吃上一碗散发着清香的“福寿面”，以这种特殊方式，表达对毛主席最浓厚的思念之情。&nbsp;&nbsp;<br />
<br />
　　吃福寿面起源于毛主席家所在的村――韶山村。毛主席逝世后，为了纪念这位伟人，村民在主席寿辰这一天自发地聚在一起，杀猪宰羊、架锅烧水，煮一碗长长的寿面，寄托对主席绵绵的思念，也默默祈祷国泰民安，人民幸福健康。渐渐地，每年的12月26日，韶山村村民们都会用吃福寿面的方式给毛主席过生日，现在已经演变成当地的一种民间风俗。从2003年开始，每一位12月26日来韶山的游客，都能在韶山的每一家饭店、宾馆免费吃上一碗热气腾腾的“福寿面”。“多吃点，多吃点，吃了福寿面，有福又长寿。”韶山村村民都会热情地劝说着游客多吃点“福寿面”。“吃碗福寿面，共念主席恩”，“主席诞辰到韶山吃福寿面”，也渐渐地被广大游客传播开来。有不少人特意在这一天赶到韶山，吃上一碗福寿面，然后再到毛主席故居和毛泽东广场向毛主席拜寿。 <br />
<br />
　　党的十七大代表、韶山村党总支书记毛雨时在现场向广大游客介绍说：“在我们韶山，每个人生日都有吃面条的习惯，长长细细的面条能表达人们幸福安康、长寿延年的美好愿望。今天是毛主席诞辰116周年纪念日，很多远道而来的游客特意选择今天来到韶山，就是为了吃一碗毛主席的福寿面，表达大家对主席的怀念之情和感恩之心。我们也会将吃福寿面的习俗一直延续下去。” <br />
<br />
　　自2005年以来，湖南郴州裕湘面业有限公司每年为“万人同吃‘福寿面’”活动提供价值十万元的特制面条。公司董事长李步凡深情地告诉记者：“吃水不忘挖井人，没有毛主席就不可能有我们今天幸福的生活。一个月前，我们裕湘人就精心为毛主席准备福寿面，看到人们都能在主席生日这天吃到有着特殊意义的福寿面，我们全体裕湘人都感到由衷的骄傲和自豪。”同时，她还说，建设好主席家乡，是对毛主席最好的纪念，今后裕湘面业将继续支持和关注韶山的发展，为伟人故里的建设出力。&nbsp; &nbsp;<br />
<br />
　　万人长跑忆伟人&nbsp;&nbsp;<br />
<br />
　　12月26日上午，纪念毛泽东同志诞辰116周年万人长跑赛在韶山举行，近万名来自省直机关单位和高校的健儿奋力拼搏，竞展风流，用这种特殊的方式怀念伟人毛泽东。 <br />
<br />
　　本次比赛由湖南省实施全民健身计划指导委员会、湖南省体育局主办，韶山市人民政府承办，湘潭市体育局协办。比赛从毛泽东同志故居出发，经北环线至火车站广场，全程5000米，设省直机关单位中、青年男、女和高校青年男、女共六个组别。 <br />
<br />
　　为响应毛主席提出的“发展体育运动，增强人民体质”的号召，深切缅怀毛主席的丰功伟绩，每年的12月26日毛主席的诞辰日，韶山都要举行纪念性的长跑比赛，今年已是第24届，而且参赛人数逐年增多，已成为韶山全民健身活动中的一个重要品牌。2004年大赛升格为省级赛事，2006年又被纳入全省全民群众健身运动项目。</div>

							
							
							
							
