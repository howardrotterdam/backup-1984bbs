													<h2>外交部对“Twitter上的中国人声援伊朗民主”表态</h2>
						
						
																											<div id="postmessage_415551" class="t_msgfont">2010年1月5日外交部发言人姜瑜举行例行记者会<br />
<a href="http://www.fmprc.gov.cn/chn/gxh/tyb/fyrbt/t649856.htm" target="_blank">http://www.fmprc.gov.cn/chn/gxh/tyb/fyrbt/t649856.htm</a><br />
<br />
2010年1月5日，外交部发言人姜瑜举行例行记者会。答问如下。<br />
<br />
　　姜瑜：女士们，先生们，新年好！现在我愿意回答各位的提问。<br />
<br />
　　问：武大伟副部长离任后是否将继续担任六方会谈中方代表？崔天凯大使担任外交部副部长后，谁将担任中国驻日本大使？<br />
<br />
　　答：我知道你关心外交部部领导的人事变动，相关安排在确定后我们会适时发布消息，建议你留意外交部网站。<br />
<br />
　　问：第一个问题，中澳天然气合同取消对中澳关系有何影响？第二个问题，近日英国《卫报》发表文章称，一些经济学家认为中国的崛起是世界经济在2007年至2009年陷入崩溃的主要原因，中国经济能否成功融入全球经济将决定世界经济成败。中方对此有何评论？<br />
<br />
　　答：第一个问题涉及中澳两国企业间的合作，我不了解具体情况。但是中澳经济互补性强，双方在互惠互利的基础上进一步加强贸易和投资合作符合双方共同利益。<br />
<br />
　　关于第二个问题，英国《卫报》日前发表文章称，一些经济学家认为中国的崛起是世界经济在2007-2009年陷入崩溃的主要原因。关于这场国际金融危机产生的根源，其实是很清楚的。我不想在此进行讨论。我只想提一个问题，过去拉美、东亚、俄罗斯等地区国家发生金融危机时，西方经济学家们一口咬定是这些国家自身造成的。这次金融危机明明发生在美欧发达国家，可西方一些经济学家不从自己身上找原因，还归咎于他人，我不知道这是什么逻辑。<br />
<br />
　　上述报道还称，中国经济能否成功融入全球经济将决定世界经济成败。我想，无论从哪个方面看，中国经济都已经深深融入了全球经济，并为世界经济发展作出了积极贡献。至于世界经济的成败，也不是取决于某一个国家，而要靠国际社会的共同努力。作为国际社会负责任的一员，中国政府将一如既往，在保持国内经济平稳较快发展的同时，与各国进一步加强合作，为世界经济可持续发展作出应有的贡献。<br />
<br />
　　问：美国国务卿克林顿称，美正与各国讨论对伊朗实施新的制裁。请介绍中方在此问题上的立场。<br />
<br />
　　答：我们注意到你刚才提及的情况。中方认为，对话和谈判是妥善解决伊核问题的正确途径，目前也仍有外交努力的空间。中方希望各方都采取更加灵活和务实的政策，加大外交努力，推动尽快复谈。<br />
<br />
　　问：第一个问题，美国一智库昨天发表报告称，中美关系恶化将成为2010年最大的政治风险。中方对此有何评论？第二个问题，请详细介绍杨洁篪外长访问非洲的日程安排。<br />
<br />
　　答：中美两国在涉及人类和平与发展的一系列重大问题上拥有广泛、共同的利益，双方都应从战略高度和长远角度把握两国关系，尊重彼此核心利益和重大关切，加强对话交流与合作，妥善处理分歧。<br />
<br />
　　我注意到该智库发表的报告称，中美关系将在台湾问题、涉藏问题和经贸关系等方面面临挑战。台湾问题和涉藏问题都涉及中国主权和领土完整，是中方的核心利益。我们坚决反对美国向台湾售武，已就此向美方提出严正交涉，敦促美方认清售台武器的严重危害性，恪守中美三个联合公报，特别是“八・一七公报”的原则，撤销有关售台武器计划，停止售台武器，以免影响中美合作大局。<br />
<br />
　　关于达赖在国际上窜访的问题，我们一贯坚决反对达赖在国际上窜访，坚决反对外国政要以任何形式和名义与他进行接触。<br />
<br />
　　关于中美经贸关系，我们认为双方应更加坚定地反对和抵制贸易保护主义，维护中美经贸关系健康发展。<br />
<br />
　　关于杨洁篪外长出访亚非六国和顺访马尔代夫的日程安排，今天早上杨外长已经离京前往马尔代夫，下午将与纳希德总统会见，晚上与沙希德外长会谈。杨外长将于1月6日至7日访问肯尼亚，8日至9日访问尼日利亚，9日至10日访问塞拉利昂，10日至11日访问阿尔及利亚，11日至12日访问摩洛哥，12日至13日访问沙特。 <br />
<br />
　　问：有中国网民在网站上声援伊朗民主派，中方对此有何评论？<br />
<br />
　　<strong>答：我们希望看到伊朗社会保持稳定、团结。</strong><br />
<br />
问：阿克毛被处决后，中国政府是否作出公开或私下的努力以修补中英或中欧关系？阿克毛案也使中国的贸易伙伴担忧，认为这是中国发出的一个信号，即中国是不能轻易挑战的大国。中方对此作何反应？<br />
<br />
　　答：关于阿克毛案，我们已经多次阐明中方立场。中国司法机关严格依法审理此案。阿克毛的诉讼权利、应有待遇和民族宗教习惯都得到了充分保障和尊重。外界应充分尊重中国司法机关依法办案，任何人都没有权利对中国的司法主权妄加评论。<br />
<br />
　　关于中英关系，我们已多次向英方表明立场。阿克毛案是一起孤立的刑事案件。两国对一些问题有不同看法，应本着相互尊重的态度妥善解决，避免对双边关系造成损害。中方重视发展同英国的关系，我们认为保持中英关系的平稳发展符合两国的利益。<br />
<br />
　　我们对欧盟轮值主席国瑞典去年底发表声明对此案说三道四表示强烈不满和坚决反对，希望欧方谨言慎行，切实尊重中国的司法主权，以实际行动维护来之不易的中欧关系良好局面。<br />
<br />
　　问：越南谴责中国在西沙群岛发展旅游业的计划，中方有何评论？<br />
<br />
　　答：我们注意到了有关情况。我想强调，中国对包括西沙、南沙在内的南海诸岛拥有无可争辩的主权。<br />
<br />
　　问：杨外长每年首次出访都选择非洲，这样的传统是什么时候开始的？为什么确立了这样的传统？<br />
<br />
　　答：今年已是中国外长连续第二十年于年初首访非洲。中非传统友好，中国高度重视中非关系，双方致力于建立政治上平等互利、经济上合作共赢、文化上交流互鉴的新型战略伙伴关系。中非战略互信不断增强，互利务实合作不断提升，在国际事务中的配合和协调更加密切。去年11月中非合作论坛第四届部长级会议在埃及成功举行，双方就新形势下进一步发展中非关系进行了全面规划，确立了发展方向和重点合作领域。中国政府在会上宣布了对非合作的新八项举措。我们将继续本着真诚友好、平等相待、互利互惠、共同发展的原则，与非洲国家共同努力，落实好第四届部长级会议的后续行动，不断推进中非新型战略伙伴关系，使中非合作成果更多更好地惠及中非双方人民。<br />
<br />
　　问：据日本媒体报道，中国政府自去年4月份开始禁止“脱北者”出境，请证实？第二个问题，朝鲜最近发表新年贺词，表达与韩国和美国对话的愿望，中方对此有何评论？<br />
<br />
　　答：非法入境朝鲜人违反中国法律，理应由中方依法处理。外国驻华使领馆不应对其进行收容、庇护。<br />
<br />
