													<h2>社科院发布2010蓝皮书 中国不稳定因素剧增</h2>
						
						
																											<div id="postmessage_360638" class="t_msgfont">　　我国房价收入比超出合理承受范围，85%家庭无能力买房。<br />
<br />
　　今天上午，中国社科院在京发布2010年《经济蓝皮书》，记者从中了解到上述信息。<br />
视频：社科院经济蓝皮书建议将房价计入CPI范畴 来源：东方卫视《东方新闻》<br />
<br />
　　蓝皮书指出，2009年中国房价太高，是政府的土地供应行政制度、对开发商囤地涨价谋利行为打击不力等深层次原因造成的。<br />
<br />
　　蓝皮书同时表示，2010年的销售面积和房价水平，主要取决于政府稳定房价的决心和出台的调控改革措施。预计住宅需求会有一定幅度的上升，房价将温和上涨。<br />
<br />
　　点评专家<br />
<br />
　　周天勇 知名经济学家、中共中央党校教授<br />
<br />
　　梁爽 住房和城乡建设部政策研究中心博士、副研究员<br />
<br />
　　张立群 国务院发展研究中心宏观经济研究部研究员<br />
<br />
　　2010年展望<br />
<br />
　　关键词・房价趋势<br />
<br />
　　预计房价不会快速上涨<br />
<br />
　　蓝皮书指出，2010年销售面积和房价水平，主要取决于政府的表态与行动，包括可能出台改革政策等影响因素。<br />
<br />
　　蓝皮书还指出，2009年房地产建设速度较快，2010年下半年以后住房供给将较快增长，综合供求情况较稳定，预计房价不会快速上涨。<br />
<br />
　　蓝皮书表示，如果严厉限制投资型和投机性住宅需求，并改革出让金为房产税，房价企稳和微微下调，并且房地产的成本有所下降，预计住宅使用需求会有一定幅度的上升，而投资型和投机性需求会相应萎缩。<br />
<br />
　　【专家解析】<br />
<br />
　　得看改革决心的有无和大小<br />
<br />
　　专家组认为，我国蓄积性的刚性买房需求在2009年已经释放，投机投资性买房需求已受到控制，未来房价将温和上涨。<br />
<br />
　　专家组明确表示，房地产及其财政体制不改革肯定不行，中国的房地产在2010年已经到了一个十字路口，2010年及其后房地产走势会怎样，主要看各方利益博弈的结果。<br />
<br />
　　专家组特别强调，这主要得看国家和政府各部分联动配套改革的决心有无和大小。<br />
<br />
　　专家组指出，在2011年和2012年，我国可进行收取房产税的试点改革，并在全国实施。<br />
<br />
　　关键词・土地供应<br />
<br />
　　政府寡头垄断土地供给市场<br />
<br />
　　蓝皮书指出，中国土地的供给市场，除了政府行政限制土地的供应量外，另一特征就是政府寡头垄断。<br />
<br />
　　据了解，这种垄断既是政府一家寡头垄断供应土地，也存在许多房地产商和其他用地商高价竞争购买土地。<br />
<br />
　　蓝皮书指出，政府寡头垄断型卖地和多个竞争者买地的土地市场格局，是除了土地供应小于土地需求外推动房地产价格持续上升的重要原因。<br />
<br />
　　【专家解析】<br />
<br />
　　改革“招拍挂”形成竞争<br />
<br />
　　对于2010年的土地调控，专家指出，应增加土地供应，抑制土地和房屋涨价，另外，还要改革招拍挂制度，形成竞争性的土地供应市场，以抑制国有垄断资本频繁制造地王。此外还要改革政府现行土地收入流程，调整房产税等来源。<br />
<br />
　　对于改革为竞争性土地供应市场，专家组明确指出了改革内容：缩小非公益用地的征用范围，集体土地只要符合建设规划，不经过政府征用，就可以直接进入建设市场。在时间上改变招拍挂不定时并集中上市供应的形式。<br />
<br />
　　专家组同时建议，城镇用地单位要转让自己使用土地的，要自主在交易中心挂拍寻求买家。<br />
<br />
　　2009年聚焦<br />
<br />
　　关键词・住房购买力<br />
<br />
　　家庭收入全款买房不现实<br />
<br />
　　蓝皮书表示，房价收入比(住房价格与城市居民家庭年收入之比)应当在3-6倍的范围，6倍以上的居民购买住房就已非常困难，也有研究认为，房价收入比在6-7倍区间。<br />
<br />
　　根据这一区间，蓝皮书指出，2009年城镇居民收入房价比将达到8.3倍，大大超出合理的承受范围。<br />
<br />
　　蓝皮书指出，2009年农民工的房价收入比为22.08倍，而对于农民的城镇房价收入比则为29.44倍。<br />
<br />
　　蓝皮书特别强调，85%的家庭没有购买住宅的能力<br />
<br />
　　【专家解析】<br />
<br />
　　房价使农民工成不了新市民<br />
<br />
　　专家组分析，据2008年全国不同收入阶层房价收入比示意图显示，包括中等收入居民在内的中低收入居民要占全体居民的60%，这些居民没有购买住宅的能力。<br />
<br />
　　专家组指出，如果再加上每年需要向城镇转移的农民人口，的确有超八成的家庭没有购买住宅的能力。<br />
<br />
　　专家组强调，这样的高房价使农民工永远也进不了城市，成不了新市民。如此高的房价收入比、居民的高成本已成为比户籍制度还要难的阻碍农民工永久进入城市和城镇的高门槛。<br />
<br />
　　专家组表示，中国正处于城市化阶段，大量需要在城镇购买房屋的是从农村向城镇转移的农民工家庭，房价与他们的收入相比2007年就已在22倍以上。<br />
