　　关键词・房价涨幅<br />
<br />
　　2009年商品房涨价近一成<br />
<br />
　　蓝皮书指出，2009年房地产经济形势从年初的低迷，走向5月份以后的高涨，8月底以后，销售增幅下降。<br />
<br />
　　而对于2009年中国房价为什么这样高，舆论大部分指责开发商，包括一些经济学家也批评房地产商谋取暴利。<br />
<br />
　　蓝皮书指出，2009年1-8月，全国完成房地产开发投资2.11万亿元，比上年同期增长14.7%，商品房销售总额23463.74亿元，同比增长69.9%。<br />
<br />
　　蓝皮书表示，2009年商品房价格上涨5%-10%，每平方米价格为4935-5280元。<br />
<br />
　　【专家解析】<br />
<br />
