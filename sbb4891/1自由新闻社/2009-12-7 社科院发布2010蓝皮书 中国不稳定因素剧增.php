													<h2>2010蓝皮书直指中国三大挑战:利益集团与民争利（社科院终于含蓄地说了点人话）</h2>
						
						
																											<div id="postmessage_388670" class="t_msgfont">昨日，中国社会科学院社会学研究所和社会科学文献出版社在京联合发布《社会蓝皮书――2010年中国社会形势分析与预测》(下称“蓝皮书”)，其中在对社会形势作出乐观预测的同时，也指出中国社会面临的一些问题和挑战。<br />
<br />
财富分配不公加剧<br />
<br />
据第一财经日报报道，蓝皮书指出，尽管人均GDP快速增长，但是居民间的收入分配却越来越不公平。《社会蓝皮书》主编、中国社会科学院社会学研究所所长李培林表示，虽然这些年来采取了一系列措施，包括大规模的财政上的转移支付，试图缩小城乡和区域之间收入差距不断扩大的趋势，但是现在城乡收入差距还是朝着扩大的方向在发展。<br />
<br />
而在影响收入差距的所有因素中，城乡差距是影响收入差距的最大因素。<br />
<br />
尽管2008年首次出现了十几年来城镇居民和农民收入增速几乎相等的局面，而且城乡收入水平的绝对比第一次出现了略微缩小的状况，但是在2009年，收入差距重新开始扩大。<br />
<br />
根据国家发改委社会研究所所长杨宜勇在蓝皮书中提供的数据，2008年城镇人均可支配收入与农村人均纯收入的比例达到331.49%，即城镇居民收入相当于农村居民收入的三倍多。<br />
<br />
李培林说，在金融危机的冲击下，因为农民工的收入情况受到了比较大的冲击，农民工的收入水平在下降，农民工外出打工收入下降，对农民工的收入增长产生了比较大的影响。<br />
<br />
李培林表示，估计今年全年城市居民收入可以增长到10%左右，但是农民人均纯收入大概增长只有6%~7%，所以城乡之间收入的增长率又拉开了三四个百分点。这个情况是中国克服收入差距不断扩大趋势的新的难点。<br />
<br />
金融危机带来治安压力<br />
<br />
由公安部办公厅统计处处长樊在勤、公安部办公厅统计处副处长宋尔东等人撰写的分报告《2009年社会治安状况分析》(下称《分析》)显示，2009年1~10月，全国公安机关共立各类刑事犯罪案件444.3万起，比2008年同期上升14.8%。全国有25个省、自治区、直辖市公安机关刑事案件立案数与2008年同期相比有所上升。<br />
<br />
《分析》称，金融危机带来的影响和冲击，在一定程度上加剧了中国经济、社会发展不协调，诱发、滋生违法犯罪的消极因素有所增多，给社会治安稳定带来新的压力。<br />
<br />
首先表现在盗窃、抢劫、抢夺等侵财违法犯罪活动出现多发、高发，并拉动刑事犯罪总量继续增长。<br />
<br />
《分析》认为，由于社会保障尚未完全覆盖进城务工的农民工，打工收入是其维持生计的基本保障。一旦遭遇下岗失业，很快就会陷入衣食无着、朝不保夕的窘迫境地，容易从事偷窃、盗窃或冲动性抢劫、抢夺等侵财违法犯罪活动。<br />
<br />
统计数据显示，前十月公安机关共立侵财犯罪案件386万起，比2008年同期上升了16.1%，其中，5000元以下的侵财犯罪案件比2008年同期多立案40.8万起，上升了16.9%。<br />
<br />
《分析》认为，虽然小额侵财犯罪侵犯对象价值较低、侵财数额不大、社会危害性较小，但具有多发、高发的特征，按照目前趋势，将成为拉动2009年刑事案件总量上升的主要原因。<br />
<br />
利益集团与民争利<br />
<br />
最高人民检察院高级检察官文盛堂撰写的分报告《当前反腐倡廉形势与前瞻》(下称“报告”)认为，当前存在少数机构和利益集团与民争利，某些“清水衙门”水不清以及有的部门和行业监管乏力的情况。<br />
<br />
报告认为，目前政治体制改革滞后，公权划分及职权配置远未科学合理到位，加上监督制约机制不完善，从而导致某些机关、部门、行业和系统腐败案件易发多发，大案要案居高不下，必须深化改革，否则积重难返。<br />
<br />
报告说，某些“清水衙门”水不清。如教育、卫生、殡葬等部门曾一向被公认为“清水衙门”，而这些部门获得经济创收权能之后，广大群众一度深感“上不起学、看不起病、死不起人”。<br />
<br />
近年来，高校成为腐败犯罪新的高发区，各地大学屡屡曝出腐败案件。同济大学原副校长吴世明因受贿罪被判有期徒刑10年，南京财经大学原副校长刘代宁被控受贿160多万元，湖北已有多所大学的主管官员因腐败落马，陕西近3年查处80多宗大学腐败案件，倒下7名厅级校官，人数之多，金额之巨，腐败情节之恶劣，都前所未见。<br />
<br />
<br />
强势权力机关、部门和单位还可以利用权力进行名目繁多的收费和办实体创收(上缴收益、管理费等)。这种“权力变现”的灰色收入加上“权钱交易”的非法收入，使少数强势权力机构拥有源源不断的预算外资金进入“小金库”，其中一部分用于单位全员福利，如代缴个税、滥发津补贴等;另一部分用于单位领导及少数实权人物奢侈的职务消费、贪污私分等，不仅直接产生腐败，而且导致机关和国有企事业单位等机构形成“油水衙门”与“清水衙门”的区别。<br />
<br />
