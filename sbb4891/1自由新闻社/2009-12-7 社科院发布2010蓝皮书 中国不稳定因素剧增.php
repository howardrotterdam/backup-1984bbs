<br />
特别是引人注目的态势在重演过去的群体性事件,比如湖北石首事件,几乎重演了2008年瓮安事件的发展。也就是说,瓮安事件是一个中学女学生溺水死亡,湖北是青年厨师跳楼死亡。和瓮安事件同样的是,大家在议论死亡的时候,政府宣布的是一种非正常死亡,但是群众通过网络等各方面开始传播,说他们到底是自杀还是他杀。后来政府通过验尸认为是自杀,家属就不相信,然后就开始护尸行动,长达30个小时。开始时地区检验不行,然后到省里进行检验,最后也是酿成了上万人的群体性事件,发生了烧毁楼房、汽车事件,发生了严重的警民冲突。当然,这次时间的暴烈程度不像瓮安事件那么强烈,但是它发生的过程、逻辑几乎和瓮安事件是一样的。<br />
<br />
我们把这类事件叫做“非阶层性的、无直接利益的群体性冲突”。非阶级性的是说参加的人群来自社会各个方面,无直接利益的是参加群体性事件的人和事件本身没有关系。这就说明在我们的一些地方,由于基层财政的薄弱,特别是改革开放30年的过程当中,在加速发展和转型的过程当中,积累了很多历史上的矛盾和问题。比如企业改制、房屋拆迁、土地征用、集资等等,这些事情当中都向群众欠了很多债,这些问题得不到及时解决,造成的民怨太深。所以一旦突发事件产生以后,就造成了所谓的非阶层性的、无直接利益性的群体冲突。 (文章来源:中国网)</div>

							
							
							
							
