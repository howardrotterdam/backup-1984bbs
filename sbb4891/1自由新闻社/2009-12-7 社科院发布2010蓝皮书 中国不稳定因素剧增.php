													<h2>社科院：19岁以下人口性别比严重失衡 男光棍将凸显</h2>
						
						
																											<div id="postmessage_390423" class="t_msgfont">社科院：19岁以下人口性别比严重失衡 男光棍将凸显<br />
2009年12月23日 08:10北京日报<br />
社科院12月21日发布的2010年《社会蓝皮书》中指出，当前我国人口金字塔底部正在加速收缩，19岁以下年龄段人口性别比严重失衡，其中0至4岁儿童性别比已升至123.26，即每100个0至4岁女孩相应有123.26个同年龄段的男孩。<br />
<br />
进入21世纪以来，我国人口金字塔底部的收缩日趋明显：全国出生人口在2002年为1647万人，2005年为1617万人，2008年为1608万人；减去死亡人口的935万，2008年净增人口673万。若2009年净增人口也在700万以内，那么到2010年底，我国总人口会被控制在13.5亿左右，比全国“十一五”人口和计划生育事业发展规划所定的目标13.6亿还低。<br />
<br />
去年，我国0至4岁人口占总人口比例为5.13%，5至9岁人口占5.46%，10至14岁人口占6.73%，15至19岁年龄段人口占7.87%。由此可见，在人口金字塔底部，年龄越小，人口占比越低。究其原因，中国社科院人口与劳动经济研究所研究员张翼分析，计划生育政策的持续性约束导致了出生率与新生儿数量的下降；再有就是生活、教育和就业成本及社会保障等问题导致人们生育观念发生变化，从而使得生育水平下降。<br />
<br />
引发问题1：养老支持体系脆弱<br />
<br />
蓝皮书指出，截至目前，我国绝大多数城市的人口生育水平低于政策生育率，直接导致了部分城市户籍人口的负增长，社会老龄化水平越来越高。专家分析，在独生子女越来越多的情况下，城市的家庭养老体系变得极其脆弱，一旦独生子女出现伤残事件，其父母的家庭养老支持体系就会断裂。<br />
<br />
引发问题2：男多女少导致光棍增多<br />
<br />
蓝皮书统计：去年我国0至4岁人口的性别比为123.26，5至9岁人口为121.4，10至14岁人口为115.91，15至19岁人口为114.1，不难看出，年龄越小的出生队列，其性别比失调越严重，女性数量较男性越紧缺。专家指出，当前19岁以下人口性别比失衡，意味着该年龄段人口进入婚配年龄后，会有更多的男性面临打光棍的窘境。<br />
<br />
张翼研究员建议，在人口紧缩的趋势下，政府部门应适时改革计划生育政策，可将现行的计划生育政策修改为“一个不少、两个正好、三个禁止”。(记者刘欢)</div>

							
							
							
							
