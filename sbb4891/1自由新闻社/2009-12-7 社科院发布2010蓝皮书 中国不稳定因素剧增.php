													<h2>社科院经济蓝皮书：中国85%家庭买不起房</h2>
						
						
																											<div id="postmessage_362858" class="t_msgfont">中国85%家庭买不起房 房价远远超出家庭收入<br />
<br />
2009年12月08日09:36<br />
<br />
　　中国社科院预计今年中国GDP增速会达到8.3%，明年会回升到9%。昨天，中国社科文献出版社发布了2010年中国经济形势分析与预测的经济蓝皮书。蓝皮书表示，全国85%家庭买不起房，房价远远超出家庭收入，土地招拍挂制度导致寡头垄断。<br />
<br />
　　蓝皮书分析，从季度经济运行情况看，2009年国民经济呈现出企稳回暖、逐步向好的发展轨迹，预测2009年中国GDP增长速度将达到8.3%左右，可以实现“保八”经济增长目标。如果2010年世界金融危机不再进一步严重恶化，国内不出现大范围严重自然灾害和其他重大问题，GDP增长率将稳步回升到9%左右的增长水平。此外，预计2010年价格水平将恢复上涨，CPI涨幅在3%以内。<br />
<br />
　　专家表示，农民工不能在城市永久定居，不仅导致劳动力供求关系的不稳定，也扭曲了农村人口的年龄结构，城市化由于没有实现农民工及其家庭的市民化是不稳定的。加快户籍制度改革，统筹城乡社会保障、公共服务和就业等一系列改革，具有前所未有的紧迫性。<br />
<br />
　　关注房价 预计房价不会快速上涨<br />
<br />
　　摘录：蓝皮书表示，房价收入比(住房价格与城市居民家庭年收入之比)应在3-6倍的范围，6倍以上居民购房就已非常困难，也有研究认为，房价收入比在6-7倍区间。根据这一区间，2009年城镇居民收入房价比将达8.3倍，大大超出合理承受范围。<br />
<br />
　　2009年农民工的房价收入比为22.08倍，对于农民的城镇房价收入比为29.44倍。85%的家庭没有购买住宅的能力。为抑制房价不断上涨趋势，政府要把城市低收入和中等收入群众住房问题从市场中抽出来，纳入政府保障范围。但对中等收入群众是“保”人人都有居住权，都能租上房，而不是“保” 人人都有房产权；而且是在政府保障下，进行市场化操作，租住房有高、中、低档，自主选择。<br />
<br />
　　2010年销售面积和房价水平主要取决于政府表态与行动。2009年房地产建设速度较快，2010年下半年后住房供给将较快增长，预计房价不会快速上涨。<br />
<br />
　　解读：中央党校教授周天勇认为，我国蓄积性的刚性买房需求已在2009年释放，投机投资性买房需求受到控制，房价将温和上涨。房地产及其财政体制不改革肯定不行，房地产在2010年已到了一个十字路口，房地产走势主要看各方利益博弈的结果。他强调，这主要得看政府各部门联动配套改革的决心有无和大小。2011年和2012年，我国可进行房产税试点改革，并在全国实施。<br />
<br />
　　政府寡头垄断土地供给市场<br />
<br />
　　摘录：蓝皮书指出，中国土地的供给市场，除政府行政限制土地的供应量外，另一特征就是政府寡头垄断。<br />
<br />
　　据了解，这种垄断既是政府一家寡头垄断供应土地，也存在许多房地产商和其他用地商高价竞争购买土地。<br />
<br />
　　蓝皮书指出，政府寡头垄断型卖地和多个竞争者买地的土地市场格局，是除了土地供应小于土地需求外推动房地产价格持续上升的重要原因。<br />
<br />
　　解读：对于2010年的土地调控，中央党校教授周天勇指出，应增加土地供应，抑制土地和房屋涨价，另外，还要改革招拍挂制度，形成竞争性的土地供应市场，以抑制国有垄断资本频繁制造地王。此外还要改革政府现行土地收入流程，调整房产税等来源。<br />
<br />
　　对于改革为竞争性土地供应市场，专家明确指出了改革内容：缩小非公益用地的征用范围，集体土地只要符合建设规划，不经过政府征用，就可以直接进入建设市场。在时间上改变招拍挂不定时并集中上市供应的形式。<br />
<br />
　　周天勇建议，城镇用地单位要转让自己使用土地的，要自主在交易中心挂拍寻求买家。<br />
