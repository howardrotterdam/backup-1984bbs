													<h2>社科院：微博客成杀伤力最强舆论载体</h2>
						
						
																											<div id="postmessage_387544" class="t_msgfont"><a href="http://news.163.com/09/1221/10/5R24E6DQ000120GU.html" target="_blank">http://news.163.com/09/1221/10/5R24E6DQ000120GU.html</a><br />
<br />
核心提示：社科院发布2010年社会蓝皮书指出，一些“意见领袖”的Twitter“跟从”者过万，对网民的感召力或煽动性极强。微博客有效地突破了某些信息屏障，赶在了传统媒体报道和政府新闻发布的前面，第一时间发布大量第一手的信息，成为杀伤力最强的舆论载体。<br />
<br />
中国网12月21日报道&nbsp;&nbsp;社科院发布2010年社会蓝皮书《2010年中国社会形势分析与预测》指出，互联网成为新闻舆论独立源头，网络结合手机成为最强舆论载体<br />
<br />
2009年12月21日，由中国社会科学院社会学研究所、社会科学文献出版社联合主办的“2010年《社会蓝皮书》发布暨中国社会形势报告会”在北京举行。会议深刻分析了2009年影响中国社会发展的重大事件和热点问题，预测了2010年我国社会发展趋势和面临的主要问题，并正式对外发布《2010 年中国社会形势分析与预测》。<br />
<br />
《社会蓝皮书》指出，2009年中，中国互联网普及率达到26%，超过世界平均水平，特别是微博客崛起，网络议题更加广泛。政府一方面强化网络管理，另一方面政府对网络舆论的反应提速，从中央到地方初步形成了政府对网络民意的监测、反馈和吸纳机制，并把网络举报列为党纪、政纪和司法监督新渠道。<br />
<br />
人民网舆情监测室对2009年77件影响力较大的社会热点事件的分析表明，其中由网络爆料而引发公众关注的有23件，约占全部事件的30%。也就是说，约三成的社会舆论因互联网而兴起，互联网成为新闻舆论独立源头。<br />
<br />
特别是在传统媒体因为种种顾虑而缺席或反应迟钝的情况下，互联网孤军深入，成为网民自发爆料和集结舆论的平台。例如2009年5月7日晚，杭州闹市区发生飙车撞人案，当地多数报纸第二天未作报道。但当晚杭州著名论坛“19楼”发帖《富家子弟把马路当F1赛道，无辜路人被撞起5米高》，回帖达到1.4 万条，网民对蔑视生命的“富二代”给予强烈谴责。加上杭州警方明显偏袒肇事车主激起众怒，BBS上网民“人肉搜索”肇事者背景，受害者谭卓的毕业院校浙江大学学子致杭州市长的公开信也从浙江大学内部论坛流传到公网，飙车案迅速成为全国关注的公共事件。<br />
<br />
此外，有线载体（互联网）和无线终端（手机）结合，网友通过手机能够进行更为便捷的信息传播，而且不限于文字，还可以将现场拍摄图片、视频上传。特别是微博客，在2009年十分活跃。微博客可通过手机或网页登陆，随时随地发布自己的所见所闻、所思所感，一次不超过140字。在微博客上跟从网民，无须对方同意。一些“意见领袖”的Twitter（总部在美国的微博客提供商）“跟从”者过万，对网民的感召力或煽动性极强。与西方微博客多谈论网民日常起居不同，中国微博客强烈关注时事。网民通过“跟从”链接而形成的微博客群落，相当于一个小型的时政新闻平台和论坛。由普通网民临时客串的“公民报道者”，可在微博客上对突发事件进行“现场直播”。例如，在石首骚乱中，一位匿名网民在饭否网上实时播报街头消息约200条。微博客有效地突破了某些信息屏障，赶在了传统媒体报道和政府新闻发布的前面，第一时间发布大量第一手的信息，成为杀伤力最强的舆论载体。<br />
<br />
互联网和无线终端嫁接的另一个产品是微视频。微视频是短则几十秒、长不过半小时的视频短片，在突发事件现场，用手机、DV等非专业设备拍摄，然后发布到网上。例如2009年元宵节晚9时左右央视新址配楼大火，第一个报道的是网民“加盐的手磨咖啡”，9时4分在天涯社区发帖《CCTV大楼元宵夜起大火了吗？？？？》，上传了手机拍摄的火灾现场照片。2009年夏天土豆网等发布了一段网民视频《北京街头最痛心的一幕》，反映一女子在协和医院门口被公交车蹭倒受伤流血，拨打120急救电话40分钟仍未得到救护。网民跟帖感叹：“当别人发生灾难，在我们眼里就是故事。岂不知，当自己发生灾难，在别人眼里也是故事。这个世界不能太冷漠！”网友信奉“无图无真相”，特别是城市管理中一些老大难问题，如城管与商贩、拆迁户与开发商冲突的现场画面传到网上，更容易激起网民的强烈不满，引爆舆论。<br />
<br />
蓝皮书还指出，根据对五大门户网站热点事件跟帖数量的统计，跟帖过万份的热点事件有5项，分别是：湖北巴东县邓玉娇案、重庆打黑风暴、云南晋宁县 “躲猫猫”事件、上海交通管理部门“钓鱼执法”、网瘾标准与治疗。这些热点事件主要涉及公民权利保护、公共权力监督、公共秩序维护和公共道德伸张等一系列重大社会公共问题，体现了广大网民积极的社会参与意识。<br />
<br />
（参见《2010年中国社会形势分析与预测》，社会科学文献出版社2009年12月出版，P246～P252）<br />
