小波还透露，自己还在这种加有一滴香的清水里掺入罂粟籽油脂粉末。许多顾客食用一段时间后就成了赶不走的回头客。<br />
<br />
明白自己的错误之后，小波痛下决心关掉了自己的小饭馆。并将这个多年深埋心底的秘密披露出来。用小波的话说，这样做的目的不仅仅是求得自己良知上的安慰，更是为了普通百姓的身体安全。<br />
<br />
记者暗访添加“一滴香”是行业公认秘笈<br />
<br />
得到小波的爆料后，记者以开米线店为名，用近一周的时间对西安市一些蔬菜副食品批发市场进行了暗访。在东郊胡家庙蔬菜食品批发市场询问销售调料的商户，多数会肯定地回答，有这种俗称“一滴香”的食品添加剂正在销售。记者看到，“一滴香”品种繁多，且均摆放在显眼处，价格在十几元到百余元不等。鸡肉味、鸭肉味、牛肉味、羊肉味等应有尽有。<br />
<br />
一位年轻的女老板说，通常都是一些米线店和火锅店、面馆的采购人员来购买这种添加剂。一瓶20元钱的“一滴香”，在小饭店可以使用一个月。这种神奇的调料加的越多，食物的香味越浓。所以饭店老板买回去到底加多少，谁也不知道。只是自己做的都是回头客的生意，而且前来询问购买的人越来越多。这位女老板销售云南和山东两个厂家的“一滴香”。她向记者介绍道，山东的货销售量很好，还是得奖产品，70元一瓶。看到记者犹豫，女老板便说：“你等等，我刚订了一批35元一瓶的，马上提货。”记者表示看看样品，这位女老板马上指着柜台上的几个小瓶子说：全是样品，免费赠送。你拿一瓶回去试试，发现生意变好了，一定会过来的。<br />
<br />
记者打开一滴香包装，立刻有一股浓烈的刺鼻味道扑面而来，让人不由得反胃。“一滴香”的颜色呈淡黄色，质感黏稠。滴一滴在清水里，本来无色无味的清水上飘起了一点油花，一股鸡肉汤的浓香立刻四散而去。<br />
<br />
“厨师也明白，类似‘一滴香’的调味品添加剂很万能，使用也很频繁，几乎成为饮食行业的必备法宝。”另一位经销户显得更有经验，表示一滴香在食物烹饪过程中随其它调料一同放进去，量不要太多，只需要一滴就可以了，保证闻起来就和真的一样。“价格便宜效果好，不少开饭馆的人都来买。若不是看到添加的整个过程，真的会以为把鲜鸡肉放在锅里，靠小火慢慢炖出来的。”<br />
<br />
“你回去搞些吃光的鸡骨头放在清水里做样子招揽顾客，然后滴进去几滴一滴香，就成了顾客想要的高汤，味道鲜得很。”这位经销户提醒道，“使用时不要让顾客看到，更不能当着顾客的面加。可以换个瓶子装或者掺在其它调料中，应付检查时，谁也不知道里面是啥。或者使用前就把标签撕掉。”<br />
<br />
记者询问“一滴香”会不会对人体有害。经销户惊讶地说：“不会呀。都在公开卖，没听到有危害的说法。这包装上写着还是得过奖的，没有哪个部门出面说不能用呀。”<br />
<br />
记者走访了西安多个小吃店，发现“一滴香”的名声在饮食行业内还真有些“威望”。不少店主表示自己听闻同行在使用，说自己尝过了，那味道确实可以以假乱真，也容易得到顾客的认可。但自己的店没有使用，不避讳地表示，因为自己不知道在哪买。<br />
<br />
记者在几家米线店看到，灶台上正炖着的一大锅鸡汤虽然色香俱全，但却是清澈见底。老板们表示，这些汤是用鸡骨粉、肉骨粉、鸡精和大料调出来的。“原料成本、烹调时间我都耗不起，只能用调味剂调制，味道和清鸡汤也没有区别。要不为啥只能看到火腿肠，却看不见鸡肉。”但这些店主都拒绝记者查看操作过程，表示这是他们店的秘密。<br />
<br />
知情人士透露，由于用“一滴香”等调味剂调制出来的汤口感鲜、成本低，所以在饮食行业的使用已成共识。如果违反“行规”，将在价格和口碑上处于劣势。“一方面的确十分方便，另一方面也降低了成本，提高了利润。现在饮食行业竞争很激烈，为了激发顾客的食欲，使用一滴香已成饮食行业公认秘笈。”<br />
<br />
在网络购物平台上，记者试着搜索“一滴香”，结果发现几个网络购物平台的“一滴香”都有不少网络商户在售卖，只是近一个月几乎没有交易量。抱着试试看的想法，记者致电几户网络卖家，但让人意外的是，尽管“前台”没有交易资料，但这些网络卖家“一滴香”买卖仍很红火。一位卖家甚至告诉记者，“一滴香”库存一直不多，订货要提前一周时间。<br />
<br />
专业人士“一滴香”是用化工原料合成<br />
<br />
记者注意到，在各种“一滴香”的外包装上，都表示选用国内最先进的生产制作工艺技术，将新鲜的骨头、骨髓，经高温浓缩萃取，通过二次调香、生香精制而成。其主要成分为酶解肉膏、水解植物蛋白、氨基酸等。并夸口产品是去腥味增香味，增强肉香味，具有香味醇厚、浓郁、酱卤肉滋味感强，香气纯正、浓郁、飘逸持久，肉感丰满、口感好、掩盖异味、耐高温的特点。主要用于肉食品加工（酱卤肉、香肠）、火锅飘香、酱菜、泡菜、凉拌菜等；或餐饮业、大酒店、家庭各种菜品的烹饪。<br />
<br />
根据网络上提供的电话，记者联系到了几家生产“一滴香”的销售人员。一开始，销售人员称，他们的产品属肉食提炼而成。当得知“是想卖米线的餐馆老板询问”时，立刻笑称他们是正规的食品添加剂生产企业，有国家质监总局和卫生厅颁发的相关证件，同时坦认“一滴香”属化工合成的食品添加剂。<br />
<br />
省质监局和食品药品监督局工作人员在电话里告诉记者，在我省尚未接到有关“一滴香”的使用咨询和相关投诉。截至目前尚未接到上级部门禁止使用销售“一滴香”的相关文件。对于“一滴香”，他们都是首次听闻，尚未熟识了解，需要实地调查和向专业检验机构求证其安全性。<br />
<br />
《中国质量报》曾报道称，食品专家基本可以断定“一滴香”是通过化工合成方法做出来的，而服用化工合成物质对人体危害很大，长期食用更会损伤肝脏，因此，长期食用“一滴香”有损健康。全国工业品生产许可查询机构也已经表示，“一滴香”属化学工业制品，食用后对人体损害非常大，会损伤肝脏，还能致癌。<br />
<br />
谁在拿老百姓健康开涮<br />
<br />
食品安全的底线一再被突破，我们要问，究竟这些商家是为了生存，迫不得已添加这些有潜在健康威胁的添加剂，还是仅仅为了赚更多的钱？也许这是一个双向的恶性循环，开始的时候是为了赚更多的钱，但当大家都这么做之后，就变成了迫不得已。如何避免这种恶性循环？仅靠事后的法律惩处和日常监管显然不能穷尽。其实，有一个重要的东西，可以治本的东西，往往被我们忽视了，那就是商业伦理。<br />
<br />
市场经济是法治经济，更是道德经济，发达的市场经济必须有先进的商业伦理为之做支撑。只有这样，市场经济才会有序、有活力，才会低成本、才会安全，才会有真正的和谐社会。因此，要从根本上解决食品安全的问题，就必须扭转社会“一切向钱看”的心态，就必须正视我国商业伦理的严重问题。<br />
<br />
在记者采访过程中，外省相继暴出“一滴香”已经广泛被应用在饮食中。而我省目前只是出现零星苗头。当诸如瘦肉精、吊白块、苏丹红以及如今正在流行的洗虾粉、一滴香等数不清的食品添加剂，或直白或隐晦让我们跨入了“民以添为食”的时代，相关部门应该有所担当，商家更应力行自律准则，切莫让百姓在一日三餐的果腹之余，为食品安全忧虑而提心吊胆。<br />
<br />
（中国质量报）</div>

							
							
							
							
