调味品是家庭必备用品<br />
<br />
新闻提示<br />
<br />
色、香、味是评价中华传统美食的基本标准，当诸如瘦肉精、吊白块、苏丹红以及如今正在流行的洗虾粉、一滴香等数不清的食品添加剂，或直白或隐晦让我们跨入了“民以添为食”的时代，食品安全忧虑已经迫使我们为一日三餐是否在果腹之余，又在威胁我们的健康而提心吊胆。<br />
<br />
在食品安全成为困扰国民生活的一大难题时，仅靠事后的法律惩处和日常监管显然不能穷尽。只有被我们忽视的商业伦理有所担当，才是实现治本的一剂良药。<br />
