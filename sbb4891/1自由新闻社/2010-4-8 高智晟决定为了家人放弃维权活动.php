													<h2>高智晟决定为了家人放弃维权活动</h2>
						
						
																											<div id="postmessage_553711" class="t_msgfont">中国维权律师高智晟失踪一年多后首次在北京接受采访，表示为了家人，他将放弃维权活动。<br />
<br />
高智晟周三（4月7日）对美联社说：“我没有能力坚持下去。一方面是我过去的经历，也正是这些经历深深地伤害了我的亲人。经过深思熟虑我最终做出的这个决定是为了寻求平静和安宁。”<br />
<br />
BBC中文网试图联系高智晟未果。但周二曾与高智晟电话通话的美国对华援助协会会长傅希秋对本网站说：“高智晟觉得自己过去的经历给家人带来巨大痛苦，这对家人太不公平。为此，他将沉寂一段时间，不再公开从事维权活动。”<br />
<br />
高智晟去年2月被警察带走后一直下落不明，国际人权组织、美英政府都对此表示关注，直到今年3月下旬有报道说高智晟出现在五台山。<br />
<br />
高智晟对美联社说：“过去几年里，经历过一些残酷的过程。但是我的经验是，所有这些过程在开始的那一天，我就把我的感情和思维给屏蔽掉了。就像一个机械功能一样，我不允许它再运动、再转。”<br />
<br />
高智晟表示不想谈论有关他失踪的事，也不想提及是否遭受中国当局的折磨虐待。<br />
<br />
他说：“我不想谈以前的事，如果我想告别过去，甚至是彻底地抛弃过去的话。过去已经不重要了。我希望有一个相对地能由我支配的未来。”<br />
<br />
高智晟在2006年底曾被中国当局以&quot;煽动颠覆国家政权罪&quot;被判刑3年，缓刑5年，剥夺政治权利1年。<br />
<br />
断线风筝<br />
<br />
高智晟是在北京的一家茶馆与美联社记者见面的，他表示这就是“聊聊”，不是采访，让人怀疑他是否继续处于中国警方的监控之下。<br />
<br />
高智晟说，他星期二晚上回到北京的家里，看到物是人非，很是伤感。<br />
<br />
“我看到我的妻子和两个孩子的鞋子，他们的鞋都放在原地未动。应该说我的感情完全失控了，因为这是我最亲的三个人，象断线的风筝一样，一年来我一点心绪都没有。”<br />
<br />
44岁的高智晟说：“未来与家人团聚，毫无疑问是我本能的目标，我希望和我的家人团聚。”<br />
<br />
美联社记者说，高智晟不再是那个“结实、不屈的维权律师”，眼前的他显得“消瘦而低调”，有时眼眶里噙着泪水。<br />
<br />
高智晟的妻子耿和以及一儿一女两个孩子目前在美国政治避难。<br />
<br />
失望与抱歉<br />
<br />
高智晟表示，他的这一决定会“让大家失望”。他说：“当他们看到我的话，他们肯定会感到失望。对他们，我表示道歉，我非常抱歉。”<br />
<br />
不过高智晟表示，他相信会有新的一批维权律师站出来，继续推动中国的合法权利和民主。<br />
<br />
“仅仅因为我的遭遇就认为其他人不会从事我所做的，这不符合人的本性。多我一个或少我一个并不重要，这些年，我听到很多人都很想去试一试，我也想与他们交谈，希望他们能吸取我的教训。</div>

							
							
							
							
