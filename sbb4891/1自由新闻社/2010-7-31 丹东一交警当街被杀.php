　　<br />
　　 这时才意识到出事了，起来趴窗户一看，马路中间一个身高大概180，便瘦，身穿白T恤衫，白裤衩，白色旅游鞋，样子看上去也就不到35岁的凶手左手持俩把刀（估计是杀猪刀，刀柄到刀尖足有40CM），右手一把，正蹲在那，用右手的刀子切（是切，不是砍，像切肉一样）一刀一刀的切交警的脖子。<br />
　　<br />
　　 听那位大婶描述，开始凶手抓住交警连捅数刀，都是致命刀，交警身中大概20刀后，曾向凶手求饶，双手朝上，但凶手并没有心软，反而更凶残，抓住交警脖领向脖子连切无数刀（横向切）。<br />
　　<br />
　　 过了大概10分钟，来了辆边防武警来了，下来了2个人，但并没有敢于上前将凶手制服，只是在围观，疏散人群，大概又过了10分钟，又来了几辆警车，大概有10多个警察，但还是没有上前制止，凶手反而更嚣张，走进马路旁边的超市拿了瓶新特贡，喝完又回超市拿了一瓶，最后搬出一箱，但并没有喝，而是拿的满酒的酒瓶砸警车，3辆警车挡风玻璃全部破碎，在砸车的过程中，围观的群众还在叫好，起哄！凶手还试图把一辆警车油箱打开，试图点燃，但庆幸的是他没有点，估计是还有意识，不想伤害周围无辜人，或是没有火机，但他的目标只是警察，至于为什么，不太清楚。<br />
　　<br />
　　 大概20分钟后，警察派来了狙击手待命，但是法律是不允许狙击手随便开枪，只有在凶手不从，或是有人质的话，才会击毙凶手。就这样双方对峙了10多分钟，一名警察跑到了国土资源局院里绕过栅栏，隔着路边的一辆警车，在栅栏里面用枪瞄准凶手，开了3枪示警，凶手并没有束手就擒，反而和警察示威，挑衅，还用酒瓶不时朝警察扔去。<br />
　　<br />
　　 这时来了一个骑白色摩托车的中年男子，大概是凶手的哥哥，他上前制止，因为距离远，我只隐约听到几句对白，凶手哥哥：“你干什么，你彪吗？你看你给人捅的。”凶手喊到：“怎么了，他们该死，警察都该死，反正我杀了一个，再杀几个更好，上来一个我杀一个。”然后这位凶手的哥哥走向被残害的交警身边蹲下摸了摸交警的脉搏，对着警察喊到：“你们在那干什么，打120啊，还有脉搏。”这时激怒了凶手，凶手回过头喊到：“没死？他必须得死！”然后走过去跨过被害人又补2刀。<br />
　　<br />
　　 这时凶手的哥哥也被吓跑了，就在这时一位警察绕过了凶手躲避枪的警车，向凶手开了第1枪，但是这第1枪让我捏了把汗，因为这枪子弹没打出来（是个哑炮），幸好又接连开了3枪，第1枪打到了凶手的肚子上，凶手抱住肚子转身，然后背后又中两枪，这时冲来了几个便衣警察，手持铁锹，把凶手制服，凶手并没有死，被120急救车拉去急救。但是那位被害的交警，在我趴窗的时候就已经因脖子断裂死亡了。<br />
