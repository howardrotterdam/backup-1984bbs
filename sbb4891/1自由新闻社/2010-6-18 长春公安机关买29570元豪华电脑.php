													<h2>长春公安机关买29570元豪华电脑</h2>
						
						
																											<div id="postmessage_689892" class="t_msgfont">2010-06-18 05:56:59　来源: 中国网　<br />
核心提示：近日，长春市政府采购网站显示，该市公安机关以29570元的价格采购一台索尼牌笔记本电脑。统计显示：从2010年1月1日至今，已发布了307条采购中标公告，采购规模238802473元。长春市相关方面并未就此做出解释。<br />
<br />
<br />
长春市政府采购网站截图<br />
<br />
中国网6月18日报道 网民自觉围观政府采购公告，从中发现问题，是一个有趣的现象。以前，网民关注的对象多是中国政府采购网、财政部等中央部委网站，最近则转移到了地方政府。<br />
<br />
5月26日，长春市政府采购网站的一条采购成交公告在网上受到质疑，公告显示，长春市公安局机关(本级)采购了一台索尼笔记本电脑，成交价格达到了29570元DD这也是网民质疑的焦点。<br />
<br />
虽然部分IT产品销售网站上该款配置电脑的价格最低才17000元左右，但索尼官方网站上的报价显示，其在线定购价格为29999元，特供套装的价格则还要高出500元。相比于后者，这次采购的还节省了近500元。实际上，29570元的价格也是此次采购中4家供应商报价最低的。值得注意的是，这台电脑的采购经过了2次采购询价，第一次由于参与报价的供应商不足3家而废标。<br />
<br />
在一些网民看来，比起有些部委机关动辄花3000多万元建个网站的采购案例，就最终成交价格而言，长春市公安机关的这次采购花费似乎并不离谱。然而，让他们感到困惑的是，政府办公电脑是否需要如此豪华的配置?<br />
<br />
<br />
按照网民猜测，之所以出现这样的现象，很可能与采购商品的确定者对电脑配置不了解所致。一个细节是，在索尼官方网站上，这款电脑的配置明细表中有关硬盘的介绍是：512GB*3固态硬盘(SerialATA)，而到了采购询价表描述中，却变成了“3块512G固态硬盘”。“其中‘*3’为备注3的意思，政府采购时竟然理解为‘3块512G固态硬盘’。真的太搞笑了。对这台电脑的配置都不了解，就花3万块去采购。”网民称。<br />
<br />
“只买贵的不买对的。”有网民跟帖评论说。“感谢长春市政府为国家节省了429元。”另一名网民调侃说。<br />
<br />
长春市政府采购网站的统计显示：从2010年1月1日至今，已发布了307条采购中标公告，采购规模238802473元。<br />
<br />
截至昨日，长春市相关方面并未就此做出解释。<br />
<br />
(本文来源：中国网 )</div>

							
							
							
							
