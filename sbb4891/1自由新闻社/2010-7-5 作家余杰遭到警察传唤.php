													<h2>一篇旧文</h2>
						
						
																											<div id="postmessage_724082" class="t_msgfont">余杰印象<br />
<br />
下午到三味书屋喝茶，听讲座，见到了余杰。<br />
<br />
阳光从窗口斜照进三味书屋，洒在余杰虔诚而坚定的脸上，古色古香的屋里四处飘散着茶香。听着余杰的演讲，我思绪良多，仿佛回到了第一次看到他书时的那个下午。<br />
<br />
初识余杰，缘于1999年的《火与冰――一个北大怪才的抽屉文学》，还记得当时一口气看到深夜的场景，他当时给我的心灵带来了极大的震憾。在自己有限的阅读范围之内，从未曾见过思想如此尖锐的文字，当时惊叹，原来世界上竟然还有这样的书。<br />
<br />
书是阿康给我的，他也是受到了极大的震憾，迫不及待的要我看，“奇文共欣赏”本是读书的一大乐趣。于是余杰成了我们的精神粮食，我们迅速地喜欢上这个新时代的年轻偶像，开始满世界找他的书，一本一本的读，读完之后深夜深夜的谈。《火与冰》之后，有《铁屋中的呐喊》、《爱与痛的边缘》、《老鼠爱大米》等等，我们越看越觉得此人真是才气纵横，笔锋老辣，旁征博引如信手拈来，十步杀人以无形之剑。<br />
<br />
直到他的《香草山》，正是在看《香草山》时关于他的负面评价也逐渐地传入我们的耳朵。《香草山》里第一次出现了他的爱情，第一次见到了他的散文，说实话，他的爱情与散文都不是怎么能吸引人的。他失去了一惯以来的尖锐与深遂，于是他也失去了我们，因为我们不需要温情的余杰，我们其实是将自己对世界的批判转嫁到了余杰身上，希望他来充当这个批判者。后来看到余杰的照片，一脸文静与秀气，不是我们想像中的“狰狰铁骨”样，心想这样的人怎么能够望鲁迅之项背？这一时期，抵销掉我们对他好印象的还有他与余秋雨在“忏悔”问题上的纠缠不清，以及他们后来的和解，导致我们认为他不是一个彻底的人，远不如鲁迅的“我一个也不饶恕”之掷地有声。后来的工作分配事件，更是让我们觉得他气短。于是在心里与这位曾经激起我们少年风茂的人渐行渐远。<br />
<br />
《香草山》之后，我们再没有看过他的书和文章，但是他却从未有完全从我们的视野中消失过，可能是因为他太有名气了，比如逛书店时你总会在畅销书架上看到他的几本或新或旧的书，比如泡论坛时你总会发现那里有关于余杰之是与非的争论。去年，一次逛书店，发现他又出了一本新书《光与影》，不知出于什么样的目的，我突然决定买下来，这本书讲的是他的美国游历，因为对他的散文本来就不怎么欣赏，再加上在此之前我刚看过林达的美国三部曲，所以觉得余杰的这本书无论从风格上还是语言上似乎都有很重的模仿痕迹，或许他的散文风格就是那样，但是因为他的《光与影》与林达三部曲差距太大，再加上余杰那有点过份显露的盛赞美国的样子，让我觉得他像是在妞妮作态，所以那本书我是以批判的方式读完的，书中划写了很多我对他的批判甚至嘲讽。当然，可能以自己的浅薄来批判余杰本身就是一件可笑的事情，但是不管我有没有资格来评判他，我只是在自己的思想观念、精神世界中对他进行了批判，与余杰无关，也与他人无关，我想这样思想的自由我还是应该有的。<br />
<br />
来到北京后，接触了一些网络名人，一次在与其中的几位聊天时问他们对余杰怎么看？他们说他们从来不看余杰的书。<br />
<br />
余杰成了与我的思想无关的人。<br />
<br />
看来文如其人指的只能是性格和思想，而不能指外貌。当你通过文字对一个人了解很多时，你往往会想像那个人长成什么样子，而往往你的全无根据的想像会与现实情况有着极大的反差，就像我无法将罗刚与他的声音联系起来一样，我也很难将眼前的余杰与他那激扬的文字联系起来。<br />
<br />
余杰其貌不扬，带着几分南方人特有的文气与秀气。余杰的口才并不好，他的演讲并不像他的文字一样激情四溢，洋洋洒洒如长河大江。可能正是这样，成就了他演讲的严谨与深刻。他讲话时不时会有短暂的停顿，用以思索最恰当的措词，而经过暂停后用出来的词你只能用精辟与恰当来形容，并且他很少用重复的词语。余杰的演讲并不激扬，但时时透露出他的理性、睿智与思索。正如一位网友所说，余杰的演讲只要记录下来，不用整理，就是一篇很好的杂文。<br />
<br />
余杰今天讲座的题目是《矿难与生命伦理》。这是他两年来在中国大陆第一次公开发表言论。<br />
<br />
他说他的父亲是一个大型国有矿山的工程师，他小时候是在矿场长大，经常目睹矿难的发生，最让他难忘也给他童年留下了很大阴影的是当矿难发生后，从井下挖出来的尸体停放在矿场中心的一个小操场上，家属去认尸时的悲惨境况。在一堆面目全非的尸体当中去认领亲人的尸体！这是怎样的一种生命中无法承受的惨痛啊。<br />
<br />
接着余杰花5分钟时间读了他从大陆官方媒体正式报道中总结出来的一份2005年矿难报表，一连串的数字听得大家心情沉重，因为在整个2005年，平均每5天中国就会发生一起死亡10人以上的矿难。<br />
<br />
余杰认为，矿难的频发，血泪的多见，已经造成了整个社会的麻木，政府麻木了，而最可悲的是民众也麻木了。这背后，最深层次的原因是生命伦理的沦丧与缺失：我们不再敬畏生命。<br />
<br />
现行的政治体制是矿难频发的直接原因，因为官员不用向下负责，而只要向上负责就可以了，而向上负责最拿得出手的就是GDP，而在GDP增长的压力下，官员没有精力也没有动力来整治矿山，只要能够增长GDP，死点人算什么？只要自己能够升迁，矿工的生死与他何涉？并且，现在的官员已经完全丧失了道德，或者是他们自身的堕落，或者是制度促使他们堕落，反正他们现在已经是完全没有道德的担当。八十年代到九十年代初，官员们的思想中，至少还是有一点“造福一方”的思想的，有些人也如那样做了，如前广东省委书记任仲夷，如赵~紫~阳。而现在的官员已经根本连这最起码的底线都丧失怠尽了，现在的情况是，谁最腐败，那么谁就得升迁。<br />
<br />
矿难的频发凸现出我们这个社会生命伦理的缺失，这是可悲的，也是可怕的，它将带来极其恶劣的后果，而这恶果将由我们每一个人承担。追其思想渊源，余杰谈到左右近现代人类走向的两种“主义”，一种是尼采主义，主张“超人”理论，认为人是可驱使与奴役的，人是工具的。由尼采主义诞生了法西斯主义、共产主义，催生了马克思、希特勒、墨索里尼、列宁、斯大林等典型的人。另一种是托尔斯泰主义，主张和平，认为这世界应当由爱来主导，人本身即是目的。由托尔斯泰主义而生的史怀泽对托尔斯泰主义发挥为“敬畏生命”，这一主义使基督教文明得以一种新的方式传承。<br />
<br />
余杰是一个基督徒。是不是所有的基督徒都有一颗博大而虔诚的心？别人我不知道，但是我看余杰有，因为他说，让我们每个人都来一点一点的努力，让我们从敬畏生命开始，为重建我们这个社会的道德，为振救苦难深重的祖国。正式演讲的最后，余杰说：“可能有人会笑我自不量力，会笑我天真幼稚，笑我迂腐，但是，我愿意与所有有志的人一起，来试一试。”<br />
<br />
正式演讲结束后，大家不限主题的与余杰进行了广泛深入的交流，这也让我从不同侧面更加深入的了解了一下余杰。有人关切的问起余杰的近况，余杰说他现在逐渐为当局所不能融，近两年来其文章已不能在大陆公开发表。出了一本书，但是在香港出版的，他说他的文章本是写给中国这片苦难深重的土地和人民，但是现在中国人却无法看到。<br />
<br />
关于写作方式，余杰说他自己也正在思索这样的一个问题，即是写不朽之文章还是总是写没多少深度和历史价值的杂文，余杰说他从来就没有把自己定位为一个大师，他希望写“速朽”的文章（鲁迅语），希望能够用他的杂文去促使不合理的现状得到改变，即使文章再无价值，但文章在失去价值同时社会得到了进步，则他心愿足亦。<br />
<br />
关于反日。他说在他写了《铁与犁》之后，这几年来也进行了反思，也去日本访问过，思想发生了一些转变，他反对过激的民族主义。他说去了日本之后，才知道“参拜靖国神社”与以前想像的有很大差距，日本靖国神社里并非供奉着像中国亡人的灵牌一样的写着死者名字的牌位，根本就没有那样的牌位存在，有的是一本大册子，上面写着日本历次战争中战死者的名字，小泉参拜的正是那本册子。真正让人担心的是靖国神社旁边有一个小庙宇，那里面是极端右翼分子的闹场。在谈这个问题时，余杰还说到解决外交问题首先要解决自己的问题，在我们中国自己最大的“靖国神社”还在受着千百万人朝拜的时候，要求日本官员停止参拜显然是不现实的。<br />
<br />
关于皈依宗教与思想自由，余杰引用蒋~彦~永大夫对他说的一句话，也是燕京大学的校训：因真理，得自由，以服务。因为真理，才可得到自由，用以服务社会和人类。<br />
<br />
仁者无忧，智者无惑，勇者无惧。余杰成熟了，博大了，深刻了，敬畏了，理性了，爱了……<br />
<br />
2006年1月7日<br />
<br />
后记：<br />
今天去论坛，看到讲稿已经帖出来了。自己的理解有限，有的地方记忆有误，下面这段话是余杰的结语。<br />
今天的中国是尼采的中国，是“狼图腾”的中国；而不是托尔斯泰的中国和史怀泽的中国。为了扭转这样的局面，我们必须像史怀泽所说的那样，“变成另外一种人”，以另一种伦理观念来组建我们的社会，以另一种伦理观念来处理我们与其他同类、其他生物和自然界的关系――“由于敬畏生命的伦理学，我们与宇宙建立了一种精神关系。我们由此而体验到的内心生活，给予我们创造一种精神的、伦理的文化的意志和能力，这种文化将使我们以一种比过去更高的方式生存和活动于世。由于敬畏生命的伦理学，我们成了另一种人。”尽管这是一种最为笨拙的办法，但我愿意跟大家一起去尝试。</div>

							
							
							
							
