作者：老虎庙<br />
北京，西山，福田公墓。<br />
<br />
　　去过西山，您不定去过福田，这个不奇怪，去公墓的事情对谁来说都不算是平常。<br />
<br />
　　佳佳因了和我住一大院儿，虽从前不能对话――就我这个辈分。也就是上班路上从他家楼下走过，这样的机会也几年里不少，且每天必经。<br />
<br />
　　楼下的院子里开着家湖南餐馆，佳佳的事情闹得纷纷扬扬的那些个日子，我常去餐馆坐坐。我问过老板，从前见过他吗？并且为她仔细描述他的模样儿。老板只摇头，不言语。我又问服务员，服务员也摇头，多问了几句，小姐就说“老板叮嘱过……”叮嘱什么？小姐没再有下半句……<br />
<br />
　　后来有许多记者来餐馆进食，有一搭没一搭地，望着窗外没有新闻的院子，看起来很无奈。一家电视台把临时转播车开到了楼下，全天候守候，成了成本最大和失望最大的一家。院子里好像莫事情无有。记者也问过餐馆老板“那孩子你认识吧？见过吧？”老板只摇头，不言语……<br />
<br />
　　四年来，我相机不离身，记下了许多的我们社会。有一天下班，天也擦黑，我过佳佳楼下，见有不熟悉的车子在楼下停靠，我随手拍了几张。就见车上跳下几人，质问我在干什么？只好当面删除，否则我能脱身？我绕道餐馆吃晚饭。餐馆老板问我发生了什么？这回是我摇头，我忽然有点怨怪餐馆老板，总似乎和大家隔膜，现在问我有何用？老板却意外说“我是做生意的。”回头去了调理间，撂下一脑门子疑惑的我……<br />
<br />
　　南方来的记者在院子里占优势，敢问，问得勤（后来他们真的就写了，发表在自己家的报刊上）。问我是不是找找餐馆的老板聊聊？我说不可以，心里想：反正我不抱希望。因为我想起他们说过“我们是生意人”，果然记者吃了闭门羹。记者说这些人很麻木，我说也难怪，“他们要做生意！很多的老百姓也这样。”<br />
<br />
　　我天天从他家楼下走过，楼下的修车师傅对我说，我忙，外头有啥消息告诉我一声。我惊讶他也记这事儿，师傅说“我住对面楼，夜里撒尿总要望望对过儿，看灯亮没有，看有什么异常……”后来我和师傅熟了，师傅说以前有过同样遭遇，所以很想看看国家怎么处理这事儿。我给师傅递烟，师傅不要，我说去对面餐馆喝点儿，师傅听了摇头，我问为甚？师傅说“院子里不太平，那餐馆老板嘛……我可没有说什么啊！”<br />
<br />
　　有一天晚上，餐馆来了许多人，忆通的李劲松、刘晓原，艾未未和赵赵，后来跟来了北青报和法晚报的女记者，还有佳佳他爸……进门的时候，我没看见老板。吃到一半儿，老板来了，我注意了看她，依旧平常着脸儿，依旧平常着声儿，只是说了句不太平常的话儿――好好侍候这些好人！<br />
<br />
<br />
　　27日，佳佳下葬后几日，我和文侠荔蕻、诗人阿尔去福田公墓祭扫。而此行刘 亚 玲带来的话却只有简单俩字“去吧……”压抑着巨大的心理负担，却没有痛快地哪怕半点其它表示。<br />
<br />
　　返回途中和农大才女教授朱毅共餐，这个年轻人如是说――如此祭奠不啻为对所有的逝者，一切都已往事。最后的当事者无论有多大的相互针对，而我们活着的却只剩大爱于他们。所有的无辜者让我们反思的不是恩怨的继续，而事情却要我们去想想：这些都为了什么？因为什么？（根据回忆而大意）</div>

							
							
							
							
