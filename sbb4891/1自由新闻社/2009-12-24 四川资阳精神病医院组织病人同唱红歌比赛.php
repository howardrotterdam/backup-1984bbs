													<h2>用唱红歌战胜精神病</h2>
						
						
																											<div id="postmessage_394257" class="t_msgfont">资阳市精神病医院举行医护人员和住院病人同唱红歌比赛<br />
<br />
文章来源：资阳日报 <br />
 <br />
一场别开生面的歌咏比赛<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;―――市精神病医院举行医护人员和住院病人同唱红歌比赛<br />
<br />
■ 本报记者 陈源根<br />
<br />
<br />
　　“没有共产党就没有新中国，共产党辛劳为民族，共产党他一心救中国，他指给了人民解放的道路，他领导中国走向光明……”“总想对你表白我的心情是多么豪迈，总想对你倾诉我对生活是多么热爱，勤劳勇敢的中国人意气风发走进新时代，啊!我们意气风发走进新时代……”<br />
<br />
　　歌声嘹亮，激情四射，雄浑悦耳！<br />
<br />
　　11月26日下午，在资阳城北莲花山上的市精神病医院举行了一次别开生面的歌咏比赛。医护人员和精神病住院病人同台演唱，由于病人不能在卡拉OK乐曲伴奏下演唱，因此一律清唱。但是令人吃惊的是他们的歌声整齐、嘹亮、悦耳、动听。他们豪情满怀，饱含深情，用自己的歌喉歌唱祖国，展示了对党的忠诚之心，热爱之情，用动听的歌声唱出了市精神病医院的风采，唱出了对党的热爱，对祖国的敬意。<br />
<br />
　　这天，虽然天气寒冷，可住院病人们的心里却暖意融融。因为歌声抒发感情，歌喉放飞心灵。<br />
<br />
　　据医院有关负责人介绍，该院开展丰富多彩的工娱疗法，对恢复病人功能，重返社会起到了重要作用。唱歌就是调节情绪的有效手段，是一种康复训练。今年是新中国成立60周年，全国上下都唱红歌，病人也想通过唱红歌来表达热爱祖国之情。医院决定以病区为单位，组织一次精神病人唱红歌比赛，激发医护人员和病人爱党、爱国热情，沟通医生和病人的关系，搭建通往和谐的桥梁。<br />
<br />
　　各病区医护人员积极教唱红歌，通过两个多月的努力，终于收到了满意的效果。这次唱红歌比赛，共唱14首，并请来我市文艺界的专家出任评委，进行公开、公正、公平评分，最后评出一、二、三等奖、组织奖各1名，分别给予了奖励。这次比赛，医护人员与病员同台演唱，并在合唱结束后，两名医护人员登台独唱，展示才华。<br />
<br />
　　市民政局十分关心关爱精神病人这个弱势群体，主要领导和有关负责人前来观赏了唱红歌比赛，给予了高度评价，认为在此次活动中医护人员和病人一起演唱，创新了形式，增强了凝聚力,缩短了医护人员与病人间的距离，达到了感情的融合。精神病人在歌唱中获得愉悦放松，这是典型的精神疗法，是对精神病人是一次“红色教育” 。<br />
<br />
　　一场别开生面的歌咏比赛落下了帷幕，但这次比赛将在永远烙印在精神病人的脑海里！<br />
