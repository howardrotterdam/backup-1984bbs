													<h2>四川资阳精神病医院举行唱红歌比赛(图)</h2>
						
						
																											<div id="postmessage_395338" class="t_msgfont">四川资阳精神病医院举行唱红歌比赛(图)<br />
<br />
2009-12-25 08:45:00　来源: 红网-潇湘晨报(长沙)　跟贴 330 条 手机看新闻<br />
核心提示：11月26日下午，四川省资阳市精神病医院举行了一次别开生面的歌咏比赛，病人想通过唱红歌来表达热爱祖国之情。该市主要领导和有关负责人前来观赏比赛，并请来市文艺界的专家出任评委，进行公开、公正、公平评分，最后评出一、二、三等奖。<br />
