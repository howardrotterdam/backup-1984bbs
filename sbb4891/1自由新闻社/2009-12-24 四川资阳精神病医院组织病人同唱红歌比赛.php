													<h2>组织一群精神病 集体讴歌共产党</h2>
						
						
																											<div id="postmessage_395779" class="t_msgfont">市精神病医院医护人员和住院患者同唱红歌比赛<br />
<br />
发布时间：2009-12-10 9:46:07 被阅览数：1484 次 来源：资阳市民政局<br />
<br />
&nbsp; &nbsp; 为庆祝伟大祖国母亲60华诞，丰富住院精神病人的文化生活， 11月26日，在市精神病医院操场举行了“医护人员和住院患者同唱红歌比赛”。此次比赛由医院策划组织，精神科全体医护人员和住院患者积极参与。他们用美妙的歌声，表达自己对祖国的无限热爱之情，向祖国送上一份诚挚的祝福。<br />
<br />
&nbsp; &nbsp; 该次比赛得到了市委宣传部、市民政局的大力支持，市民政局党委书记、局长、老龄办主任何正月，市民政局党委委员、副局长纪昌兴等领导亲临现场与广大医护人 员及住院患者共同观看了红歌演唱比赛。<br />
<br />
通过本次比赛，极大的调动了住院患者们的热情，培养了大家的团队意识和集体荣誉感，同时也是一次重要的爱国主义、集体主义和民族精神教育，对我 院的康复治疗与共建和谐医院起了积极的推动作用。<br />
<br />
&nbsp; &nbsp; 全体参赛住院患者精神饱满，士气高昂，用他们整齐的队列，飒爽的英姿、嘹亮的歌声接受医院特邀的市文艺界专家评委的检阅。让《没有共产党就没有新中国》、《走进新时代》、《阿瓦人民唱新歌》等洋溢着爱国主义激情的歌曲在操场上飞扬。女康复病区演唱的《北京的金山上》和《阿瓦人民唱新歌》，以别出心裁的二重唱，整齐的用手击拍，从参赛队中脱颖而出，一举夺得一等奖。此外，男治疗二病区、老年精神科二病区分别荣获二、三等奖，康复科荣获本次比赛的组织奖。<br />
