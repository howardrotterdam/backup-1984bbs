													<h2>靠毛泽东思想治好精神病</h2>
						
						
																											<div id="postmessage_393636" class="t_msgfont">(人民日报　1971年8月10日) <br />
<br />
　　中国人民解放军一六五医院医疗组和湖南省郴州地区精神病院的医务人员，用毛泽东思想指导医疗实践，为医治好精神病闯出了一条新路。两年多来，这些医务人员坚持用毛泽东思想教育病人，辅以中西医结合的办法进行治疗，使许多精神病人恢复了健康，重新走上了三大革命斗争第一线。他们中的一些人，有的已被评为学习毛泽东思想积极分子。 <br />
<br />
　　重新认识精神病 <br />
<br />
　　中国人民解放军一六五医院医疗组，怀着捍卫毛主席无产阶级卫生路线的战斗豪情，于１９６９年４月，来到彬州地区精神病院。过去，这个精神病院由于受叛徒、内*、工贼刘少奇反革命修正主义卫生路线的毒害，对精神病人，从治疗到管理，完全照搬资产阶级“专家”“权威”那一套，长期使用“电休克”、“胰岛素休克”和“大剂量冬眠灵”等所谓“三大法宝”治病，使患病的阶级兄弟受到折磨。医疗组的同志和医院革命医务人员一起，愤怒揭发，批判了刘少奇推行反革命修正主义路线的滔天罪行，决心走自己的路，为患精神病的阶级兄弟解除痛苦。<br />
<br />
　　要治好精神病，首先要弄清精神病到底是怎么一回事？他们遵照毛主席关于“人的认识一点也不能离开实践”的教导，对病人的情况进行了认真的调查研究，掌握了许多第一手资料。在实践过程中，许多事实引起了大家的深思。医疗组来到病房时，许多病人听说亲人解放军来给大家治病，纷纷围拢来高呼：“毛主席万岁！毛主席万万岁！”有个狂躁病人，发病时把公家的棉被撕得粉碎，但她却不撕自己的衣服。<br />
<br />
　　这些现象说明什么问题呢？医疗组和医院的革命医务人员打开毛主席著作，寻求答案。毛主席教导说：“在阶级社会中，每一个人都在一定的阶级地位中生活，各种思想无不打上阶级的烙印。”大家认识到：精神病人都是阶级的人、社会的人，用阶级和阶级斗争的观点去分析精神病人的言行，就可以发现，绝大多数病人的病态和他们的阶级地位、社会生活是一致的。向解放军表达热爱毛主席的思想感情的病人，大多数出身好，平时表现也好。而那个毁坏公物的病人，一查问，原来她出身剥削阶级家庭，世界观根本没有得到改造。这些人为什么会发生精神病呢？医疗组和医院领导组织革命医务人员，用阶级斗争的观点，抓住典型病例进行病因分析。有一位女病人，经过几个月的治疗，没有痊愈。医疗组护士徐桂兰便和她促膝谈心，引导她谈自己的发病经过，帮助她挖病根。原来，这个病人的未婚夫从外地来信说，他被分配去做炊事工作了。这个病人觉得自己不光彩，思想上产生了激烈的斗争，引起大脑功能紊乱，得了精神病。针对这个情况，徐桂兰就和她一起学习毛主席的光辉著作，狠批叛徒、内*、工贼刘少奇散布的“读书做官论”等反革命修正主义谬论，帮助她解开思想疙瘩。经过教育和帮助，病人主动写信给她的未婚夫，要他向张思德同志学习，全心全意为人民服务。病根挖掉了，加上经过新针治疗，这位病人很快痊愈出院了。 <br />
<br />
　　从许多这样的病例中，大家清楚地看到：许多精神病人得病的原因，是在头脑中公与私的激烈斗争中，陷在“私”字的圈子里，一时想不开，造成大脑部分功能紊乱，失去了正常控制思维和管理全身各部分机体的能力。许多病人痊愈后也深有体会地说：往“公”字上想，越想越清楚；往“私”字上想，越想越糊涂。由于 “私”字严重作怪，遇到了问题想不通，往往几天几夜不能吃饭睡觉，精神就逐渐不正常了。精神的东西要靠精神力量来战胜一六五医院医疗组和郴州地区精神病院的医务人员，在运用辩证唯物论的认识论重新认识精神病之后，对于治疗精神病靠什么的问题，又进行了进一步的探索。他们反复学习毛主席关于“外因是变化的条件，内因是变化的根据，外因通过内因而起作用”的伟大教导，认识到各种各样引起精神病人发病的事是外因，决定地引起大脑功能紊乱而使病人发病的，则是人的世界观，是内因。因此，要治好精神病，主要的因素是做好病人世界观的转变工作。他们说：精神的东西主要靠精神力量来战胜，治好精神病主要靠战无不胜的毛泽东思想。<br />
<br />
　　他们把用毛泽东思想教育病人贯穿在治疗精神病的全过程中。当病人入院时，他们首先做好调查工作，弄清患者的家庭出身、社会经历、发病原因和病人的思想，并且组织病人学习毛主席著作，有针对性地组织病人开忆苦会、批判会，举办各种类型的学习班，密切结合实际，向病人进行阶级教育、形势教育、路线斗争教育、人生观教育、同疾病作斗争等方面的教育；并且还根据病人各种不同的发病原因，进行具体分析，发动职工、家属和陪病人的亲属共同做过细的思想教育工作，攻破病人头脑里“私”字的顽固堡垒，从根本上治疗他们的病。有个贫农的女儿，先后６次发病，在一个大医院治疗，仅“电休克”就做过２０多次，但病一直没有治好。有人说，这个病人在发病的糊涂阶段，神志不清，大脑功能紊乱，对她灌输毛泽东思想不可能生效。医疗组和医院的革命医务人员学习毛主席的光辉哲学思想，认为这种“无效论”是一种形而上学的看法。用毛主席关于“一分为二”的观点看问题，任何精神病人的大脑功能有紊乱的部分，也有不紊乱的部分。病人发病期间，神志有不清醒的一面，也有清醒和比较清醒的一面。只要掌握住每个病人发病的特点和病情变化发展的规律，抓住病人清醒和比较清醒的一面，对症下药做工作，就可以促使病人不清醒的一面向着清醒的一面转化，从思想上和生理上治好他们的病。于是，医疗组女卫生员曹东平主动同这个贫农的女儿结成“对子”。病人越狂躁，她工作越耐心；病人越不理睬，她越去亲近她。天天同病人一起学习毛主席著作，一次又一次地和病人促膝谈心，终于触动了病人的思想。在一次忆苦会上，这个病人流着眼泪，说出了自己发病的原因。曹东平抓住她不狂躁的短暂时间，针对她的病因，和她共同学习毛主席的有关语录，让她在学习会上自觉地斗私批修，狠触灵魂，狠抓世界观的改造。只经过３个多月的治疗，这个女病人就痊愈了。她出院后，不但没有复发，而且思想、工作表现都很好，接连两次被评为先进职工。<br />
<br />
　　通过许多这样的医疗实践，医疗组和这个医院的医务人员更加坚定了用毛泽东思想去攻克精神病的决心和信心。他们知道，做病人的思想工作，要比做正常人的思想工作困难得多，决不是一朝一夕之功所能奏效的。但是，只要医务人员坚持不懈地把全部心血倾注在用毛泽东思想教育精神病人上，就一定会取得胜利。有的医务人员同忧郁型病人一连谈心几十次，病人毫无表情。但是他们毫不灰心，坚信毛泽东思想的威力，继续坚持做耐心细致的思想教育工作，终于使患者恢复了健康，重新踏上了三大革命斗争第一线。精神病院女医生范菊和，为了治好一个患忧郁型精神病的女病人，满怀无产阶级深情，搬进病房，和病人同住一间房，同睡一张床，经过４０多天的艰苦工作，终于摸清了病人的思想活动规律。于是，她有针对性地选了一些毛主席语录念给病人听，并且结合新针和药物治疗，使病人病情逐渐好转。医疗组长顾嘉炎，为了治好一个拒食多日、表现近于木僵状态的病人，组织医务人员进行了认真的调查研究，发现病人拒食的原因可能是怀疑饭中有毒。后来在给病人喂饭时，顾嘉炎就亲自吃给病人看，解除了病人的疑虑，使他大口大口地吃了起来。接着，又针对病人的思想疙瘩，同他一起学习毛主席著作，对他进行忆苦思甜教育，逐渐提高了他的思想觉悟，渐渐给他治好了病。当医务人员送他回家时，他的亲人和村子里的贫下中农激动地连声高呼：“毛主席万岁！毛主席万万岁！”为了调动病人同疾病作斗争的主观能动性，医疗组和医院领导还从病情好转的病人中选拔骨干分子，依靠他们抓好病人的“学习日”，办好各种类型的毛泽东思想学习班，开展革命大批判，组织病人参加适当的生产劳动和开展文艺体育活动，使病人一直生活在团结、紧张、严肃、活泼的政治气氛中，增强了战胜疾病的决心和信心。一些病情好转的病人，在病房门口贴了一幅对联，表示战胜疾病的决心：“斗私批修，彻底改造世界观，灭资兴无，坚决战胜精神病。” <br />
<br />
　　用毛泽东思想创造治疗精神病的新医药学通过两年来的医疗实践，医疗组和郴州地区精神病院的医务人员深深体会到，用毛泽东思想教育病人、管理病人，是治疗精神病的根本。但是，精神病究竟不同于一般的“思想病”，它已由精神的矛盾运动引起了部分大脑功能紊乱。因此，在坚持用毛泽东思想武装病人头脑，改造病人世界观的同时，还必须辅以药物治疗。<br />
<br />
　　用什么药好呢？医疗组和郴州地区精神病院的医务人员，遵照毛主席关于“古为今用，洋为中用”，“推陈出新”的方针，决心创造中西医结合的治疗精神病的新医药学。他们对中医和西医的治疗方法以及中药、西药，都采取取其精华、去其糟粕的态度，在不断实践、不断总结的基础上，吸收两者的长处，创造新的治疗精神病的医药学。为了摸索新针治疗精神病的穴位，医疗组和医院革命职工宁肯在自己身上试扎千次，不愿在病人身上错扎一针。医院领导干部董秉余，５０多岁了，还坚持在自己身上练针。他们从几十个穴位中，选了十多个穴位，根据不同病情进行扎针，效果很好。与此同时，他们又认真试用中草药来治疗精神病。他们翻山越岭，看图对药，采回了大量草药。为了病人的安全，每一个药方他们自己都要先尝过，终于摸索出了几种治疗精神病的有效药方。 <br />
<br />
