广州残杀流浪猫狗迎亚运<br />
<br />
<img src="https://yopic.us/images/15cc5p.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
广州亚运前大肆捕杀流浪猫狗。有关部门日前在海珠区设置捕杀笼，里面放上食物，流浪猫狗进入后笼子会自动关上，猫狗被抓后送到小区偏僻处屠杀，不少广州市民怒斥手法残忍，直呼受不了。有市民更发起「拆笼」行动，见一个拆一个。<br />
<br />
「搞个亚运也不用这样吧！」近日海珠区各住宅小区不少地方设置了捕抓流浪猫狗的笼子，市民陈小姐表示，捕抓行动在本周初开始，前天她在家中，突然外面一连串狗仔惨叫声，冲出门一看，原来一只经常在小区流连的流浪狗被两名公安网住（图）。<br />
<br />
公安其后将狗仔丢上货车运走，狗仔在车上依然「呜呜」地惨叫，状甚可怜。有网民则表示，为配合清理流浪猫狗，当局在区内部份偏僻处增设屠场，猫狗被捕后即送往屠杀。<br />
<br />
广州为举办亚运，今年以来大肆修路致交通挤塞、沙尘满天，前天更强硬清拆城中村，令民怨沸腾。对于捕杀流浪猫狗的行为，市民骂声一片：「简直令人发指，残忍至极！」、「嬲到想讲粗口」，有网民表示，流浪猫狗的生命也是生命，不应随意屠杀。<br />
<br />
有网民前天发起救救猫狗行动，呼吁市民看到捕杀笼时要把笼子踏烂丢进垃圾桶。有人更公布捕杀笼的位置，呼吁市民前往「破坏」，「海珠区润汇大厦后面、紫金大街单车棚、穗花小区、沙园小区、玫瑰园对着的江边都有，位置很隐蔽」。<br />
