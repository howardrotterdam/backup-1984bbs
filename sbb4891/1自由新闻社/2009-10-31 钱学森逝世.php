<br />
科学的计算告诉人们：还远得很！今后，通过农民的创造和农业科学工作者的努力，将会大大突破今天的丰产成绩。因为，农业生产的最终极限决定于每年单位面积上的太阳光能,如果把这个光能换算农产品,要比现在的丰产量高出很多。现在我们来算一算：把每年射到一亩地上的太阳光能的30％作为植物以利用的部分，而植物利用这些太阳光能把空气里的二氧化碳和水分制造成自己的养料，供给自己发育、生长结实，再把其中的五分之一算是可吃的粮食，那么稻麦每年的亩产量就不仅仅是现在的两千多斤或三千多斤，而是两千多斤的20多倍！<br />
<br />
这并不是空谈。举一个例：今年河南有些特别丰产试验田要在一亩地里收一百六十万斤蔬菜。虽说蔬菜不是粮食，但到底是亩产一百六十万斤！<br />
<br />
所以，只要我们有必需的水利、肥料等等条件，加上人们的不断创造，产量的不断提高是没有问题的。今天条件不具备，明天就会创造出来，今天还没有，明天一定会有！</div>

							
							
							
							
