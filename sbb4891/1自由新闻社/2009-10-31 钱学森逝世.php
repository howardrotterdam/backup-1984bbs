													<h2>钱学森祖先的生存智慧：活着</h2>
						
						
																											<div id="postmessage_307928" class="t_msgfont">钱B是杭州临安人，生于唐宣宗大中六年（公元852年），那正是个乱世。贩私盐出道的钱B后来从军，907年受后梁封为吴越王。此时，正是中国历史上最乱的时期――唐末五代十国。<br />
<br />
钱B的吴越国大致在今天的江苏、上海、浙江、福建东南沿海一带。称王之后，钱姓传了三代五王共执政86年。钱B不仅给家族留下了众多才智后代、给子民留下了富裕江南，他遗产中最宝贵的是两样精神财富，“钱氏家训”和“钱王遗训”，此二训诫广泛地影响着他数以万计的后人。<br />
<br />
钱B在今天被称为“杭州之父”，他对杭州的营造开始于1100多年前任杭州刺史期间，史料记载，钱武肃王的赋税很重，这些“重税”后来用于筑堤和疏浚。杭州钱B研究会秘书长钱刚说，“从这些税的用途看是功在千秋的”。钱B初称吴越王时，中国东南版图上还没有一条叫“钱塘江”的江流，那时，这条江叫“之江”――今天浙江卫视的蓝色LOGO就是江水的“之”字形走势。<br />
<br />
之江经年泛滥，民不聊生，钱B筑堤改恶患为良灌，因此，受到堤坝护佑的当地人就改称之江为“钱塘江”。在吴越国富甲东南上千年后，今天的杭州已从西湖时代跨入钱江时代。杭州市把钱塘江沿岸建成杭州的“外滩”，钱江新城等CBD区域，确立了杭州的未来。<br />
<br />
中国历代的战火多燃起在中原，很少烧到江浙闵一带，这也是东南一隅常年安康的政治保障，这一切都与钱B生前留下遗训有必然联系。<br />
<br />
钱B的政治远见在于他留下《钱王遗训》，“度德量力，而识时务，如遇真主，宜速归附。圣人云顺天者存。又云民为贵，社稷次之，免动干戈即所以爱民也。如违吾语，立见消亡；依我训言，世代可受光荣”。他告诉后人，要审时度势，如果有人统一了中原，我们吴越之国不要再去和人家战火争斗，而要诚心诚意地归顺过去。江南这些年不容易，不要再毁于战火了，民生最珍贵，江山社稷反在其次；不听我的话很快就会灭亡，记住我的训戒，钱氏家族世世代代无穷匮也。<br />
<br />
所以，当他的孙子钱弘m看到赵匡胤建立了大宋，便顺应大势选择了北归。为了消除赵的疑虑，他在投奔北宋时还带了一支2500名包括文官武将和眷属在内的队伍。杭州钱B研究会副秘书长钱怀荣介绍：宝石山上的保m塔就是为钱弘m已经离杭赴卞京而建，杭州百姓害怕赵匡胤会杀了他，专门建塔取名“保m”寄托心愿。<br />
<br />
赵匡胤也没亏待钱氏，宋朝初年排“百家姓”时，赵姓天下第一、钱家就是一姓之下百姓之上。这一支人马后来定居中原，从家谱世系上看，中原很多城市都有钱王的后人。“纳土归宋”的政治智慧远远超过大宋上两个朝代爱江山不爱亲人者李氏弟杀兄、杨氏子弑父的残暴和无情。<br />
<br />
中国前水利部部长钱正英是临安钱氏，她曾经说过：“如果不是纳土归宋，当时吴越国非要与宋朝拼个鱼死网破，皮之不存，毛将焉附，我们这些钱氏后人不知会在哪里。”<br />
<br />
钱弘m纳土归宋，不仅保住了钱家的后世百代，也为160多年后北宋不敌金军而南迁，打下了经济、政治与群众基础。南宋选择三秋桂子十里荷香的杭城，正是因了吴越国当年的民心和盛景。<br />
