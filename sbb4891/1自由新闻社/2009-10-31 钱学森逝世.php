																											<div id="postmessage_307920" class="t_msgfont">钱学森和亩产万斤<br />
<br />
钱学森的胡说八道，最搞笑的事情是在大跃进期间，论证一亩地产万斤粮。这不但是尽人皆知，而且钱老在年轻时有意无意间犯的这个错误，还时不时的被他的论敌如经济学家于光远辈，引用了来诋毁钱学森（涂建华:中国特异功能二十年）。<br />
<br />
<br />
据说大跃进能够发生的一个原因是人们的科学精神丧失殆尽，包括钱学森这样被誉为中国“导弹之父”的著名物理学家。1958年，钱学森曾在《大众科学》和《中国青年》杂志上发表了“粮食亩产会有多少？”和“农业中的力学问题”两篇文章，用植物光合作用的原理，解释了亩产百万斤的可能性。<br />
<br />
尽管当时的中国科学院对大跃进不积极，有不少农业方面的专家，包括著名的物候学家竺可桢和植物生理学家罗宗略都撰文反对。例如植物所罗所长说:“万斤粮只能用农艺学来分析，以植物生理学总结，不是从实际因素出发，不可轻信”。但钱学森这位既不是农艺学家、也不是植物生理学家的导弹专家却大胆声称:粮食产量可以无限地增加，太阳光能射到地表，只要利用其30％，亩产就可能达到“两千多斤的20多倍！”（钱学森:粮食亩产量会有多少？）。其实在农业和生物学上的事情，远没有工业生产过程和弹道导弹计算那样来的精准。而问题就出在那个光合转化效率的30％上:以目前的生产力水平，高产农田植物的光合转化效率也不过 0．5％，50年代，撑死了也不超过0．3％，而钱学森把0．3％一下子就说成30％，扩大了100倍！这本身在科学上是绝不能实现的事情，人家世界上顶尖的导弹专家就敢于拍脑袋瓜儿瞎说。<br />
<br />
一年后，钱学森的计算又使亩产潜力精确为5.85万斤(钱学森:农业中的力学问题)，从而为“大跃进”和粮食产量“放卫星”作了科学上的背书。钱先生以他科学家的名誉作担保，让千千万万的人相信“大跃进”政策的“科学性”，其效果极为恶劣（南冠客:为什么中国大陆和诺贝尔奖无缘？），也是极度不负责任的。但受此影响和激励，而且怕被拔白旗和打右派，全国大专院校和科研单位的知识分子，也先后都不争先恐后地加入到深翻土地和放人造卫星的群众运动中去。<br />
<br />
在李锐的《反思大跃进》的文章还有这样的记载，毛泽东的秘书田家英问毛泽东:“你也不是没当过农民，你应当知道亩产万斤是不可能的”。毛泽东说:这是我看了大科学家钱学森的文章，才相信的。同样的记载还有后来执掌中央文革小组的陶铸等高层领导人，也是引用了钱学森的文章（陶铸:驳“粮食增产有限论”，1958）。毛泽东和中央书记处的领导人于是便对下面虚报的粮食产量信以为真，担心起“粮食多了可怎么办呀” 的问题。后来毛泽东检讨说，他是上了科学家们的当（中国青年报:理性照耀中国）。<br />
<br />
这场唯心主义闹剧，以上千万农民的饿毙而告终。虽然当时的政治领导人毛泽东等人要为领导“大跃进”造成的大饥馑以及上千万生命的不幸死亡负主要责任，但钱学森这位身居高位、曾经获得“国家杰出贡献奖”、并在当时积极地“科学”论证了亩产上万斤的科学家，也难辞其咎。</div>

							
							
							
							
