《经济信息联播》的记者表示，中国的米、面、食用油三大粮油市场中，只剩下大米市场还处于较为松散的竞争状态，其他领域外资都已经进入。大型外资粮食企业通过价格竞争来抢夺市场，不仅让国内粮食收购企业面临生存困境，粮食加工业也将重新洗牌，今年以来，由于江西市场的外资粮食巨头益海嘉里、国有企业的中粮和中储粮等纷纷设立加工项目，导致一些民营中小粮企倒闭或被兼并。在整个江西，现有超过2000家的大米加工企业，大部分将倒闭或停产。<br />
<br />
根据报道，益海嘉里通过收购位于江西粮食主产区抚州市金溪县的江西和氏米业有限公司，成功南下投资设立米厂，组建了益海嘉里（抚州）粮油食品有限公司。<br />
<br />
报道指出，今年早稻收购季节，在金溪县国有粮食购销公司和中储粮金溪直属库尚未入市收购的情况下，益海嘉里（抚州）粮油食品有限公司8月1日率先挂牌收购早稻，每百斤出价98元人民币（下同），既高于今年国家制定每百斤93元的最低收购价，也高出去年同期江西早稻收购价，还高于目前早稻平均收购价。<br />
<br />
市场人士分析，以益海嘉里进入南方籼稻加工和收购市场为标志，外资粮油机构从大豆、小麦次第推进，按照从北向南、由次到主的顺序，已参与加工最主要的粮食消费品水稻，全面进入我国粮食收购市场。<br />
<br />
从上世纪80年代末起，居于香港的马来西亚首富郭鹤年家族系内公司发挥原料、资金、技术和管理的优势，“兵分两路”展开了对中国油脂市场的合围。它一方面通过嘉里粮油聚焦产业链后端的油脂精炼领域及销售环节，借道中粮等国有粮油企业成立了南海油脂，打造了“金龙鱼”等一系列小包装食用油品牌，迄今合计占有超过50%的市场份额，并在此过程中引领中国人食用油消费结构的转变，为其整合中上游资源打下了厚实的市场基础。<br />
<br />
另一方面通过丰益控股与在原料方面具备优势的国际四大粮商之一的美国ADM公司合资成立益海集团，布局产业链中端的压榨环节，趁2002-2003年期间大豆过山车行情导致中国大豆压榨企业几乎全军覆没之际，横扫国内中小型榨油厂，大肆进行并购，在很短的时间里成就了益海集团在中国压榨领域的霸主地位。<br />
<br />
郭鹤年家族的事业王国涵盖糖业、粮食、工业、种植、航运、矿产、房地产、国际贸易、酒店、保险、证券、建筑等行业，几乎无所不包。他也是香港英文报纸《南华早报》的大股东。</div>

							
							
							
							
