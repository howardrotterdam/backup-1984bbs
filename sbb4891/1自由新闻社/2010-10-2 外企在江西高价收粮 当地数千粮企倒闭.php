													<h2>外企在江西高价收粮 当地数千粮企倒闭</h2>
						
						
																											<div id="postmessage_852535" class="t_msgfont"><a href="http://www.taihainet.com" target="_blank">www.taihainet.com</a>　2010/10/2 6:32:37　新京报　【字体：缩小 放大】<br />
 <br />
<br />
　　本报讯 据央视《经济信息联播》报道 近日，农业部发布消息称，今年我国秋粮种植面积增加，大部分地区秋粮长势好于上年，有望再获好收成。<br />
<br />
江西抚州上演收粮大战<br />
<br />
　　记者近日在产粮大省江西省发现，现在离晚稻收割还有近一个月的时间，但一场空前的收粮大战早已开始。与夏季时早稻收购观望心态不同的是，眼下无论是外资、国有粮食企业还是民营粮食企业，都参与到高价收粮的大军中。<br />
<br />
　　江西省抚州市临川区东馆镇农民游春荣告诉记者，现在很多收购粮食的都来找他买粮食。临川区粮食购销总公司经理黄抚州证实，各地都来抢粮，有国有粮食企业、私企、外企，还有周边的省份，竞争可谓十分残酷。<br />
<br />
　　抚州市粮食局副局长张绍义介绍，抚州商品粮位列江西前茅，高达50%以上，这里成为各地前来抢粮的最前沿阵地之一。<br />
<br />
　　在收粮大战中，无论是哪一路的买家，拼的都是价格。过去参与粮食收购的主题，主要是国有粮食企业和私营粮食企业。这两年，特别是今年有的外资机构开始介入，让今年的粮食收购价格增加了变数。<br />
<br />
外企价格高出一般水平<br />
<br />
　　外资粮食企业益海嘉里，通过收购位于江西粮食主产区抚州市金溪县的江西和氏米业有限公司，成功南下投资设立米厂，组建了益海嘉里(抚州)粮油食品有限公司。<br />
<br />
　　今年早稻收购季节，在金溪县国有粮食购销公司和中储粮金溪直属库尚未入市收购的情况下，外资粮商益海嘉里(抚州)粮油食品有限公司8月1日率先挂牌收购早稻，每百斤出价98元，既高于今年国家制定每百斤93元的最低收购价，也高出去年同期江西早稻收购价，还高于目前早稻平均收购价。<br />
<br />
　　有竞争对手算了一笔账，益海嘉里收购的常规早稻水分为14.5%、杂质为3%，如果折算成13.5%水分、1.5%杂质的国家等级标准，其98元/百斤的收购价实际上达到105元/百斤。按照目前市场米价，加工100斤稻谷要亏损1―2元。<br />
<br />
　　益海嘉里(抚州)粮油食品有限公司拒绝接受采访。<br />
<br />
　　业内人士称，以益海嘉里进入南方籼稻加工和收购市场为标志，外资粮油机构从大豆、小麦次第推进，按照从北向南、由次到主的顺序，已经参与加工最主要的粮食消费品水稻，全面进入我国粮食收购市场。<br />
<br />
江西数千大米加工厂倒闭<br />
<br />
　　据记者了解，在我国米、面、食用油三大粮油市场中，只剩下大米市场还处于较为松散的竞争状态，其他领域外资都已经进入。大型外资粮食企业的大举进入，通过价格竞争来抢夺市场，这不仅让国内粮食收购企业面临生存困境，粮食加工业也将重新洗牌。<br />
<br />
　　记者采访发现，今年以来，由于江西市场的外资粮食巨头益海嘉里、国有企业的中粮和中储粮等纷纷设立加工项目，导致一些民营中小粮企倒闭或被兼并。在整个江西，现有超过2000家的大米加工企业，大部分将倒闭或停产。<br />
<br />
■ 观点<br />
专家称影响国家粮食安全<br />
<br />
　　外资机构进入粮食加工，不光导致粮食加工行业洗牌。据国家粮油交易中心的统计，截至9月5日，河北、河南、浙江等小麦和稻谷主产区，粮食经营企业累计收购量比去年明显减少，减幅达四成。有关专家认为，很多外资粮食加工企业的大量收购也是一个重要原因。<br />
<br />
　　社科院农村发展研究所研究员李国翔认为，由于预期价格上涨，大量外资收购商急于收购小麦和稻谷，所以整个收购局面发生变化。<br />
<br />
　　李国翔告诉记者，由于外资加工企业资金实力比较雄厚，经营机制比较灵活，竞争力要高于国内民营和国有加工企业，如果延续下去，无疑会对国家粮食安全构成影响。</div>

							
							
							
							
