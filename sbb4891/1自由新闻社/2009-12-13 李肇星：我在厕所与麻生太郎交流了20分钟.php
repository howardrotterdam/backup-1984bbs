													<h2>李肇星：我在厕所与麻生太郎交流了20分钟</h2>
						
						
																											<div id="postmessage_370699" class="t_msgfont">李肇星:“星爷”的幸福生活 伉俪情深为人称道<br />
2009年12月10日 10:00　来源：人民网-《环球人物》<br />
<a href="http://www.chinanews.com.cn/cul/news/2009/12-10/2010211.shtml" target="_blank">http://www.chinanews.com.cn/cul/news/2009/12-10/2010211.shtml</a><br />
<br />
<img src="http://www.chinanews.com.cn/cul/news/2009/12-10/U136P4T8D2010211F107DT20091210100306.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
频频闪烁的镁光灯下，依旧是丰富的表情，略显浓重的胶南口音，睿智、诙谐的回应，掷地有声的言辞。<br />
<br />
在《环球人物》杂志记者的采访过程中，无论是李肇星的中学同学，还是早年同事，对于李肇星走上职业外交家的道路，都笑言“没想到”：“搞文学工作才是他从小的梦想。”<br />
<br />
1982年3月，在外交部的第一次新闻发布会上，李肇星以翻译身份亮相，当时的发言人是钱其琛。1985年，李肇星作为新一任外交部发言人正式登场，好几个西方记者就“西藏问题”不断向他发难。一个记者说：“我也有自己的家乡，如果你说我的家乡不是属于我们国家的一部分，我才不在乎呢！而你为什么对‘西藏问题’这样在乎？”李肇星毫不留情地回答道：“你对自己的国家怎么看是你的问题，但是中国的主权是神圣的，外国人无权干涉！”这个记者被李肇星的犀利“镇住”了，从此心悦诚服，和李肇星成为朋友。从1985年到1990年，李肇星担任外交部发言人的5年，正是中国外交的“多事之秋”。他凭借机智、犀利的作风给海内外媒体留下了深刻的印象，从此有了“铁嘴钢牙”的美名。<br />
<br />
李肇星回忆说，为了走出困境，他和时任日本外相的麻生太郎上演了一段“洗手间外交”：“2006年中日高层关系还很僵，我和他(麻生太郎)共同出席一个国际会议。他看我上洗手间，就跟了出来，而且拦住手下没让进去。就这样，我们站在卫生间里，在没有媒体记者的打扰下，包括历史问题在内畅谈了20多分钟。这次轻松愉快的交谈，为中日两国外交领导人加深相互了解、增进互信奠定了基础。”<br />
<br />
2009年两会流行语，与胡锦涛总书记“请你指正”一同风行网上的，是网民们对李肇星的亲切称呼：“星爷”。<br />
<br />
　　当《环球人物》杂志记者询问李肇星，是否知道网友对他的昵称时，他一副少见的困惑表情：“不知道呀。”<br />
<br />
记者注意到，仅收录在2007年出版的《李肇星诗集》里的诗就有192首，而他实际创作的诗歌数量远不止这些。从莱蒙湖畔的沉思到安理会上的雄辩，从赤道战鼓到椰林呐喊，从古希腊的神话到壮观的悉尼歌剧院，都溶入了他想象的长河，化作他笔底的波澜。<br />
<br />
　　对于诗歌的品位，李肇星一向挑剔。他很少读从外文译成中文的诗，“因为稍稍读懂一点原文后，就觉得译文与原文不是一回事儿，至少不是一个味儿”。<br />
<br />
　　“万千佳丽中/唯你不施脂粉/一片纯情/光采照人……你是青春；百十山岳间/唯你天开地阔/平等中寻觅挚友/以诚实营造自我……你是中国！”<br />
<br />
　　这是李肇星创作的《青春中国》，被谱曲歌唱后，在美国引起强烈共鸣。<br />
<br />
“禾禾，亲爱的儿子，我正在地中海上空给你写信……”这是一封写于1983年7月31日的信，李肇星正在由巴黎飞往莱索托的飞机上。“使命是光荣的，我知道。但对你，却欠下了账。去东欧前答应过你，回来后一定陪你去逛北海公园，谁知居然没有时间了！这次别离，我又许下诺言：‘别难过，孩子，我会来信，很长很长的信，每月至少一封。’我是真诚的，再不会食言。”李肇星这些写自非洲的信后来辑成了《黑色，是美丽的》一本小册子。<br />
<br />
　　记者问李肇星在教育孩子上有何心得。他说：“应该是父母好好学习，孩子天天向上。父母要多向孩子学习。”<br />
<br />
　　去年，李肇星夫妇在期待多年后，终于当上了公婆。儿媳是著名歌唱家阎维文的女儿。<br />
<br />
　　谈及儿媳，李肇星满意地说：“她很孝顺，对她妈妈很好。”不过他又再次搬出《宪法》笑着说：“《宪法》第四十九条规定，父母有教育未成年子女的责任，人家是成年人了，有选择的自由，管不着了。”<br />
<br />
　“每天这么忙，还有时间写诗吗？”记者问。<br />
<br />
　　“坐飞机的时间少了，所以写的诗也少了。你不知道，我的诗大多是在飞机上写的。”李肇星说，在今年两会上，有一个台湾记者问他想不想去台湾。当时他就诗兴大发，动情地讲起了“女娲补天”的传说，并用诗的语言表达渴望去宝岛台湾一游的心愿：“那是我的一个梦想。”<br />
<br />
“前年，我访问南非，他们的外长热情邀请中国参加南非首次举办的2010年世界杯。我答应了，可是我心里没底啊！我当然希望中国男足入围，但万一入不了围呢？于是，我就带着几个人先到那个决赛场转了一圈，表示咱们中国人也来过了！”说罢，又是一个李肇星式的仰天大笑。<br />
