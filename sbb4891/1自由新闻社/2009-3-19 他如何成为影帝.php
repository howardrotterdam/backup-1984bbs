													<h2>他如何成为影帝</h2>
						
						
																											<div id="postmessage_56513" class="t_msgfont">对小组的人说，以下都是常识，但社会上还有很多人不知道。这是写给媳妇公司同事看的，那些80后小白领，听到有人叫影帝，莫名惊诧，群起攻之。我也是80后，写的有些罗嗦，只为他们能看明白一些。他们都在看，到时候收集反馈。见笑了。<br />
<br />
<br />
<br />
他如何成为影帝<br />
媳妇在公司称呼众人敬仰的温宝宝为影帝，遭到单位同事的围攻。她感觉很尴尬，回来埋怨我。是我灌输了她的这种貌似杵逆的思想。<br />
<br />
其实，从专业角度看，温氏表演功力不逊于中国的任何一位演员。人们喜爱它，无非是他有看似敦厚的性格，和蔼可亲的笑容，谦逊有礼的态度，忧国忧民的表情，以及事必躬亲的行动。特别是在抗震救灾期间，他冲在一线，亲自指挥，悲切感人。那一刻，赢得了亿万老百姓的心。<br />
<br />
他在废墟上举行新闻发布会，他在黑夜中安慰被压受伤的孩子，他在成堆的死尸前三鞠躬，他把自己都感动了，何况草民你我。<br />
<br />
其实，人类组成国家的本意，就是在无法抗拒的大灾难面前形成互救体系，以集合万众的能量抗衡大自然的无常。温氏在灾区的行为只不过完成了一个政府CEO的本职工作，却被意识形态化，塑造成神。<br />
<br />
在一个高效和公平的社会里，评价一个CEO的业绩，不应该只看他的表情有多完美，话语有多亲切。而是，他为我们做了些什么，他的成绩在哪？假若没有，那他就只是在“政治表演”，“影帝”一说由此而来。<br />
<br />
那么且让我们看看，在他最关心的社会民生领域，他做了些什么。<br />
<br />
教育。教育产业化使得掌握社会教育资源的各类公办大中小学，可以堂而皇之地向学生家长收取各种费用，择校费、扩招赞助费、借读费、补习费等等，不一而足，教育公平严重失衡。其次，政策壁垒更加森严，高考中各省市之间的不公平显而易见。还有，应试教育进一步登峰造极，素质教育空喊口号，学生苦不堪言。即使是教育部长也承认，中国教育改革基本失败。<br />
<br />
中国的大学，最大的成功就是把一群青春洋溢的孩子圈在围墙后面，没有让他们窜到大街上游荡；中国的大学最大的失败就是没有让这些孩子们在学校里没学到有用的东西，他们只是慢慢的在校园中老去，一届一届，来的时候是待耕耘的土地，走到时候仍旧是荒芜一片。<br />
<br />
去年毕业的大学生还有150多万人没找到工作，今年就又有600多万汹涌而至。<br />
<br />
面对如此地严峻的就业危机，温总做了些什么呢？他语重心长的告诉孩子们：要仰望星空！不要只是关心脚下的事情，那是没有未来的。我仰望星空，它是那样寥廓而深邃；我仰望星空，它是那样庄严而圣洁；我仰望星空，它是那样自由而宁静……<br />
<br />
没有实际措施，只会表演浪漫，是解决不了吃饭问题的吧。<br />
<br />
医疗。这里面的黑幕就更多了，作为一个社会记者，我可以跟你们举三天三夜的例子。一个总体的现象就是：人们看不起病，人们不敢生病，人们“被医疗事故（被自杀）”了也无处申冤。有些时候，富人可以花500万做天价医疗，穷人却只能等死。这么多年来，中国老百姓花在看病上的钱越来越多，但是医疗资源（医院、护士、医生）却没有多大增加，1978－2005年间，我国居民个人现金卫生支出增加了197倍，可同期医疗资源增加了2倍多一点。钱都去了哪里？公费医疗的浪费比公款吃喝还大。<br />
<br />
再看看作为政府CEO的温总做了些什么？<br />
<br />
去年，温总在天津调研，准备乘火车返京。在天津火车站，候车大厅的乘客纷纷起立鼓掌，总理和乘客们握手。李贵树夫妇带着孩子准备回家。总理朝着他们走过来。问他们，“到天津干什么来了？”回答说看病，总理又问，看了没？家长说，看不起，准备回家。 在询问完孩子的病情后，总理握住家长的手说，“来北京看吧，我给你们安排一下。”孩子被迅速送往北京儿童医院，各地官员纷纷来探望，送钱，温总理捐了1万元现金之后，没几天患者家长就收到到48万捐款。<br />
<br />
孩子准备回家等死，温救了他，但从深处讲，正是政府创立的医疗制度害死了千千万万的孩子、老人和病患。<br />
<br />
安居。100多年后，我们的后代或许会嘲笑现在的我们。在中国的各大城市，大量房子空置，却有更多的人没有房子住。一方面，我们有很多房子，一方面，大家都没有房子，即使有了的，也得为它付出大半生的按揭岁月。这是新时期的“社会怪现状”。房地产的本质是政府设租，企业寻租，居民买单。房子是搜刮民脂民膏的工具，用这个工具，我们筑起9.8%的GDP增量。我们享受经济发展的荣光，他们享受经济发展的果实。<br />
<br />
在房地产恶性发展的时候，温先生又做了些什么呢？救市，救房地产的市，把它列入经济提振计划，在两会上以法律形势固定下来。他会在镜头前关切的询问某位的住房问题，但从来都只为房地产商考虑。<br />
<br />
或许你会说，他做了很多好事，比如减免农村税赋。是的，表面上看，他废除了中国几千年的制度。但你要知道，中国已经完成了由农业国向工业国的转变，他废除的制度，是建立在农业国基础上的制度。现在他不需要农民那点辛苦钱了，因为工业税收、第三产业税收、个人所得税收，让他们的钱包鼓鼓囊囊。<br />
<br />
没有了农业税，还有大大小小的企业上缴的税收，还有每天消费品的消费税，还有几亿人民的个人所得税，甚至还有利息税，这些苛捐杂税，比农业税充裕的多吧。<br />
<br />
也许你要说，这都不是一朝一夕能解决的事，这也不是他一个人犯下的错误，但你要知道，他在位十年，就等于耽误了十年，你我有几个十年？这是典型的过渡政府形态，他这一过渡不要紧，整整耽误了一代人。<br />
<br />
这就是为什么，他被称为影帝，因为他只会做表面工作。他理应获得奥斯卡小金人，也理应在历史上获得另一种评价。<br />
