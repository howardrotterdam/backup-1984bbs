																											<div id="postmessage_56688" class="t_msgfont"><strong>悲情过头了</strong><br />
<br />
李恩,O果名采,2009-3-18<br />
<br />
据说今年温家宝总理在人大结束後的记者会上，唯一一次获得掌声的讲话内容，是他说很想到台湾去看看，虽然年纪已经大了，但「走不动，就是爬也愿意。」此语一出，获全场鼓掌，可见大多数人还是受落於被煽情的。<br />
<br />
温总理善於表现悲情，在他脸上，可以看见忧虑丶辛劳丶坚毅丶愁绪，但很难看到宽容丶愉快丶轻松。这样一个总理，让人整天感到天降了大任於他身上，就没打算让他正常为人，整天不是饿其体肤，就是劳其筋骨，没有一天安生日子可过。<br />
<br />
或许，作为中国总理实在是太辛苦。但外国领导人，比如美国总统奥巴马就不辛苦吗？<br />
<br />
<strong>人家是该严肃则严肃，该轻松则轻松。只有这样，人民才会对他的办事能力有信心。在你愁得要死的时候，他还笑得出来，那就令人相信他必有办法，有指望。要是在你刚发愁的时候，领导人已经表现得愁得要死了，脸色比你还苦，那你还有什麽指望？<br />
<br />
只有在不成熟的社会，才有人民喜欢看到领导人脸色比自己更苦，以为那便是「先天下之忧而忧」的具体表现了，才相信那是个用心的好领导。</strong><br />
<br />
所以，温总理一定是中国人民的好总理。但是，不要说是堂堂中国总理，就是一个普通老百姓，想去台湾旅行，即使老得走不动，也一定有辆轮椅坐坐吧？也一定不用悲情地「爬」吧？<br />
<br />
当年邓小平表示就算坐着轮椅也想到香港来看看，今天温总理竟然想「爬」去台湾，跟前辈比一比，成何体统！但是，此语一出，竟赢得满堂掌声，以为这便是温总理欲作台湾一游的决心，那真让人无言以对了。<br />
<br />
大家都知道温总理感性，但大家又知不知道这一次温总理感性过头了？感性过了头，便如说了满口话，不但收不回来，还易被人利用。<strong>如果台湾的民进党公然叫板：「好呀，温先生，我们欢迎你爬过来！」那国务院办公室的发言人，该如何反击呢？</strong><br />
