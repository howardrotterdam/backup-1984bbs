																											<div id="postmessage_317649" class="t_msgfont">中新网凤阳11月6日电 (记者成展鹏)11月6日晨，中国农村改革发源地凤阳县小岗村党委第一书记、村委会主任沈浩在小岗村意外去世。经公安部门现场勘验，未发现异常情况，初步判断为猝死。具体死亡原因，有关部门正在进一步调查之中。<br />
<br />
另转新华社：<br />
安徽凤阳小岗村书记：用火热青春绘就美好蓝图2008年01月30日11:11 [我来说两句(3)] [字号：大 中 小] 来源：新华网 　　新华网北京1月30日电（记者姚润丰）安徽省凤阳县小岗村之所以闻名，是因为它是中国农村改革的发源地。1978前，小岗村18那张按着18个红手印的字据成为农村改革波澜的源头。然而，事隔20多年后，小岗村人又一次在一份文书上由全村各家各户的户主按下了红手印。 <br />
 <br />
 <br />
 <br />
<br />
<br />
　　第二次按下的红手印是为了挽留从省财政厅被选派到小岗村担任党支部书记的沈浩。<br />
<br />
　　2004年初，作为安徽省第二批年青党员优秀干部，沈浩从省财政厅被选派到小岗村担任党支部书记。2006年5月，小岗村成立村党委，沈浩任小岗村党委第一书记。然而，这位从省城下派的第一书记，伴随着小岗村几年来的发展，他的路走得并不轻松。现任村党支部严宏昌和支委张秀华分别介绍说：沈浩才来时，村里人信不过。有的说，城里人受不了乡下的苦，挂个名份，待不了几天就得跑。有的说，他是借着小岗村的名气来镀金的，镀完金就走。可沈书记住下来了，一住就是几年。处处为村里谋规划、办实事，大年三十还泡在村里。平时，他办公探亲用的是自己的车，不给村里人添麻烦。单身一人，办公住宿只用一间房，那条件和他在城里的条件是不能比的，上个厕所都要跑到楼下院子外，不容易啊！<br />
<br />
　　2006年底，沈浩在小岗村任职三年届满，省城调令下来，要调沈浩回机关，于是，发生了小岗村人又一次集体按下红手印的故事。村民们要求省选派办把沈浩留在小岗村，再带领他们干三年。<br />
<br />
　　通过各种途径，村民们终于把这位领军人留下来了，小岗村人心中反而不安了：人家家中丢着80多岁的老母亲，丢着妻子和正在上学的女儿，作为一家之主，为了小岗村的发展，人家不能尽孝尽责，实在对不起人家。于是，支委们又代表全村找到沈浩，去向他表示歉意。<br />
<br />
　　沈浩留下了，这一留至少又要三年。但他很平静。“其实我已经习惯了。再留几年，一定抓好新农村建设的新契机，发展小岗村。”<br />
<br />
　　“一夜越过温饱线，但二十多年没跨过富裕坎。”人们用这句话形容这个中国改革第一村。由于满足，小岗村的脚步一度放慢，光环与荣耀也一度成为包袱，造成小岗村的徘徊。直到2003年，小岗村的人均收入仅有2000元。<br />
<br />
　　2007年4月，凤阳县将当时的小溪河镇、燃灯乡、石门山镇合并起来，组成“小溪河镇”。11月，由安徽省城乡规划设计研究院编制的《凤阳县小岗村新农村建设规划》在获得省专家组评审通过。小岗村将和周边的严岗、韩赵、梨园、石马、车庄等5个行政村合并，成立“小岗村”，形成一个面积达到29.4平方公里、2020年人口达万人的“小镇”大小的中心村，标志着小岗村将再次迎来难得的发展机遇。<br />
<br />
　　办工业、兴商贸、科学种田，以市场经济的头脑发展种植、养殖和高效农业……四年来，沈浩与小岗村两委班子团结带领112户、486名小岗人，着力实施“开发现代农业、发展旅游业、带动发展村级工业”的三步走发展战略，探索依托科技发展小岗村的路子。把江苏省张家港市长江村帮助兴建的占地面积80多亩的优质葡萄科技示范园，做大做强，现已示范带动全村95％的农户发展优质葡萄500多亩，户均4亩多优质葡萄，年人均葡萄收入2000元；小岗村还引资150万元兴建了小岗村风味猪科技养殖场，养殖场带动了该村92户村民发展风味猪养殖，年出栏仔猪5000头，年均为农户创收50余万元；2006年5月，安徽科技学院3名大学生来到小岗村发展大棚双孢菇科技示范生产，取得成功，他们与村民联合成立了小岗村食用菌专业合作社。2007年，发展标准大棚179个，棚均年效益在8000余元，双孢菇已经成为小岗村新兴支柱产业和农民收入的主要来源。<br />
<br />
　　近4年来，小岗村人均收入年递增600元以上；2007年，全村人均收入达6000元。村民们还通过“改厕、改圈、建沼气池”，建起循环利用的高效绿色农产品基地；“大包干”纪念馆红色旅游、农家乐生态游也在促进小岗村振兴发展，“大包干”纪念馆每年接待各地游客万余人次。2006年，小岗村跻身2005年度“全国十大名村”，2007年初，小岗村被授予安徽省乡村旅游示范点称号，一个美丽、和谐、富裕、文明的的社会主义新小岗重新向世人展示。<br />
<br />
　　当选全国农村基层干部新闻人物特别奖时，沈浩显得十分平静，他介绍，根据小岗村新农村建设规划，未来，小岗村在扩容的同时，还将追赶中国十大名村。规划描述的远景是：到2020年，全村生产总值达到10亿元，人均GDP为2至3万元，农民人均纯收入达到8000元左右，从温饱发展阶段快速进入宽裕发展阶段。</div>

							
							
							
							
