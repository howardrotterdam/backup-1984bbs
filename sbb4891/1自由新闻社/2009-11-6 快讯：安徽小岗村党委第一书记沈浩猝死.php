另一面的沈浩<br />
<br />
作者: 南方周末记者 潘晓凌 2009-12-23 22:41:58<br />
<br />
<br />
■记者手记<br />
<br />
采访沈浩的妻子王晓勤是最让我意外的。此前在小岗，所见所谈都是沈浩的好与伟大。村民关正银与我聊天一个多小时中，还三度落泪，说沈浩“什么都好，一点恨头都不给人留”，如今走了，“村子空荡荡的，心里也空荡荡的”。<br />
<br />
在合肥集体采访王晓勤时，这个心直口快、直接得有些刻薄的女子当着众记者的面埋怨丈夫――<br />
<br />
六年了，他回家的时间有多少？尤其是后三年，回家时间不到二十天。<br />
<br />
他心里装得下小岗，怎么就装不下我们母女俩？<br />
<br />
他在小岗受人爱戴、做得好有什么用？还不是一个乡下干部？<br />
<br />
他脑子到底是怎么想的？家庭环境那么好，住得那么舒服，也不缺钱，他偏偏要到那个黑乎乎的地方住六年！<br />
<br />
王晓勤的数落没有逻辑，想到哪说到哪，边说边不停地掉眼泪。在坐的记者，包括我，也被感染得不停地擦眼睛。在她数落完沈浩，记者提问时间时，前两分钟竟无一人回过神来提问题。<br />
<br />
其时，在被这个场面感染之余，一种交织着意外、同情甚至些许反感的复杂情绪在我心底翻腾。这绝不是一个典型的“典型人物”的妻子形象――温柔、宽容、隐忍与理解。王晓勤不能完全理解沈浩对小岗的付出与理想。<br />
<br />
那么，小岗之外的“完人”沈浩，究竟是个什么样的人？他为什么要执著于那个在妻子看来卑微得不值一提的理想？<br />
<br />
私底下，我提出去她家，王晓勤爽快的同意了。<br />
<br />
沈浩的家处于合肥市市中心，黄金地段。<br />
<br />
她带我上楼去看沈浩的书房。这是家里最大的屋子，门是两扇对开的。房间设计很别致，房间从右至左依次是高及天花板的书柜、书桌、床、浴缸。<br />
<br />
“这都是沈浩自己设计的，”王晓勤说，“他以前是个很有生活品位的人，到了小岗，又变回农民了。”<br />
<br />
坦白说，我对“城市人”沈浩的品位确实很吃惊，在村民眼里的“农村人”沈浩，拍拍门槛上的土，直接就往地上坐；饿了、渴了，顺手拿起桌上吃喝到一半的碗杯，草草地往嘴里扒；对村民递过来两块钱一包的烟，总是乐呵呵地抽一支，吧嗒吧嗒地抽起来，边抽还能边说话，绝不掉下来，像小岗人一样……<br />
<br />
王晓勤掀开床罩让我看下面的床垫与电热毯。在小岗沈浩的宿舍，我也曾做过这样的举动，下面没有电热毯。<br />
<br />
我告诉她，她又开始哭起来，“我以前也过过苦日子，可现在过得舒服了，让我回到从前，再也不可能了。可沈浩怎么能够，从富到穷，和从穷到富一样容易呢？而且，我看他是彻底融进去了……”<br />
<br />
我看着这个极真实的中年女子，无厘头地想起那部热播全国的电视剧《潜伏》，余则成起初为了恋人投奔共产党，却被分配与一个完全志不相投的陌生女子假扮夫妻十余年，周旋于上司、同事、情报、“妻子”、豪华生活与诱惑之间，斗志不移、理想始终。<br />
<br />
拿沈浩与余则成相比的确有些无厘头，以王晓勤联想到翠平更是有所不敬。但是，沈浩与余则成有一点是完全一致的，那便是对自己所选的事业与信仰的忠诚。他们各自身边戏剧性的反差与冲突，更丰富、凸显了他们内心信仰的强大与坚贞。<br />
<br />
做人如斯，不得不敬。<br />
――――――――――――<br />
和余则成一样拎不清。</div>

							
							
							
							
