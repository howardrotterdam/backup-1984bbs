													<h2>改革先锋小岗村血泪史续：村支书招商猝死酒桌</h2>
						
						
																											<div id="postmessage_318023" class="t_msgfont">安徽小岗村村支书因连续接待投资客商猝死<br />
<a href="http://news.163.com/09/1107/07/5NGJASLQ0001124J.html" target="_blank">http://news.163.com/09/1107/07/5NGJASLQ0001124J.html</a><br />
<br />
齐鲁晚报11月7日报道 11月6日晨，中国农村改革发源地凤阳县小岗村党委书记、村委会主任，45岁的沈浩在小岗村意外去世。5日上午，沈浩连续接待了三批到小岗村洽谈投资的客商，小岗村民称，沈浩当天曾大量饮酒。6日，凤阳县发布公告称，经当地警方现场勘验，未发现异常情况，初步判断为猝死，具体死亡原因正在进一步调查之中。<br />
<br />
6日，凤阳县发布初步调查报告称，11月6日早上6：40，凤阳县小岗村村民杜永兰准备找沈浩谈事，到沈浩住所后发现其在床上没有应答。杜永兰立即与房东一起找村干部汇报。村干部随即向镇主要负责同志汇报，并打电话给镇医院、派出所。<br />
<br />
早上7时许，凤阳县委接到小溪河镇党委报告称，沈浩同志可能死亡。<br />
<br />
6日，沈浩的房东吴广发告诉记者，他们也是早上6点40分许发现沈浩去世的。据吴广发介绍，沈浩2004年到小岗村担任书记时，就住在他们家的院子里。这个院子是1999年县农业局建的利民工程――农技站。吴家住在后院平房，前面是一栋两层小楼，沈浩就住在二楼一间小房内。<br />
<br />
吴广发的妻子马家献告诉记者，沈浩平时身体很好。5日下午3点左右，她在院子门口自家的门市部照看生意，看到村党委副书记张秀华扶着沈浩回家。她看到沈浩“很累的样子，要睡觉”。<br />
<br />
马家献说，沈浩日常没有公务就在他们家吃饭，每月给他们600元生活费。<br />
<br />
5日下午5点，马家献像往常一样，上楼去喊沈浩吃饭，看到沈浩在房间内“睡得呼呼的，很香”。她说，沈浩平时很累时，下午就不下来吃饭，自己冲点蛋白粉喝，并曾告诉过她，这时不要喊他吃饭，所以马家献当时就没有叫醒沈浩。<br />
<br />
马家献说，6日一早，她打了4个荷包蛋，刚做好准备给沈浩端上去。这时村里的老大妈杜永兰来找书记谈事，杜先上楼，随即发现沈浩已身亡。<br />
<br />
据了解，沈浩为安徽省财政厅派驻小岗村的挂职干部，在小岗村村民中享有很高的声誉。</div>

							
							
							
							
