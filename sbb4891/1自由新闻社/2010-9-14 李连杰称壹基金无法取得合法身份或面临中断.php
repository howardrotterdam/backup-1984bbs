													<h2>李连杰称壹基金无法取得合法身份或面临中断</h2>
						
						
																											<div id="postmessage_828806" class="t_msgfont">中新网9月14日电 国际功夫巨星李连杰在2007年创立了“壹基金”，由一位电影明星转型成为一名慈善家，李连杰近几年的主要精力，也都放在了壹基金上边。然而， 9月12日，李连杰在接受中央电视台《面对面》采访时却透露，该慈善计划目前正面临严重的危机。他说，壹基金存在中断的可能。<br />
<br />
李 连杰在节目中说，从05年筹备“壹基金”以来，他一直就是真孙子，遇到重重困难，到处求人。但他从未退缩。他曾希望一位身价上百亿的朋友能多捐点钱，但其 一番辛酸感言让他顿悟，坚定创立“壹基金”每人一块钱的想法。他梦想壹基金能独立成长，不依赖怜悯和人情。然而，壹基金现在面临中断，李连杰首次对媒体倾 诉困境。<br />
<br />
功夫皇帝为做公益到处求人<br />
<br />
《海 洋天堂》是李连杰最新主演的一部公益电影，出演这个电影，李连杰只拿一块钱的片酬。壹基金计划是李连杰在2007年成立的慈善公益计划。李连杰曾在宣传片 中说：“我一直在想，生活在这个地球上，大家就是一家人，所以我就想了一个概念：每一个人每一个月拿出一块钱来，自己人帮自己人，一加一加一等于一，每个 人每个月一块钱，大家是一家人。”<br />
<br />
壹基金项目就像是李连杰的孩子，而李连杰也就像海 洋天堂里的父亲一样，从孩子诞生之初，到成长的每一个过程，都从来没有离开过一个求字。李连杰自己曾说：“在我自己心中定位的我，是一个乞丐，是一个全球 最大的乞丐。”2007年，壹基金成立之初，李连杰应邀出席《满城尽带黄金甲》的发布会，他几乎把这个舞台当成了壹基金宣传和求人的阵地。以至于后来很多 人把那当时李连杰的表现说成是搅局。<br />
<br />
李连杰说：“有人说我是装孙子，我不是装孙子，我是真孙子。”<br />
<br />
李连杰：壹基金面临中断<br />
<br />
据央视报道，目前中国的慈善组织很不发达，到去年为止，从事慈善事业的人不到两万人，而美国慈善组织的从业人员超过900万。今年6月21日，北京师范大学壹基金公益研究院成立，这是国内第一所开展公益慈善方向高等教育的研究院。<br />
<br />
李连杰说：“未来随着《中国慈善法》等等的出台，会有更多的公益组织会出现，这些都需要专业人才去管理，可能20年内，中国要建立10所这样的大学，培养的专业人才才够用。”而李连杰本人更喜欢的不是研究，是摸着石头过河中去总结经验。<br />
<br />
然而，壹基金面临巨大发展时机的同时，也正在面临着一个生死的重大关口，采访中，李连杰透露，壹基金计划存在着中断的可能。<br />
<br />
据央视报道，中国基金会分为公募和非公募两种。公募基金有权向公众募捐，而非公募基金无权向公众募捐。中国没有民办公募慈善基金会的先例。壹基金计划就是与中国红十字签订一个三年合约计划，他虽然暂时可以向公众募款，但是没有独立的账户和公章。所以壹基金潜力巨大的手机平台的捐款，因为账户的不独立，而在操作上存在重大的不便。远远无法实现李连杰最初设计的每人每月最少捐一元，让小捐款变成大善款的理念。<br />
<br />
李连杰将壹基金形容为一个没有身份证的孩子。他说：“这三年这孩子起码还健健康康的，没出什么事，但是他没身份证。已经越来越受到希望中国公益慈善事业更加专业透明化者的质疑。”<br />
<br />
当被记者问及壹基金要中断了，李连杰能否承受时，李连杰动情的说：“其实我一直告诉自己，它有第一天，就有结束的一天。”<br />
<br />
回应“虚伪”慈善质疑<br />
<br />
李连杰进入慈善圈后，虽然荣誉和光环一直围绕，但质疑声也从未间断过。主持人柴静问到：“首先人家会说，你加入了新加坡国籍，却还要说要为中国人做慈善？”对此，李连杰回应称，其实无论你走到哪里，你永远不要做伤害这个民族和这个国家的事情，这是我的底线。<br />
<br />
谈及以什么样的身份来做慈善，李连杰说：“我在全世界所有的舞台上都说，我是一个人类。”<br />
<br />
同 时，在公益和演艺之间，李连杰一直过着截然不同的两种生活，出演《海洋天堂》他只拿一块钱，但演完公益的片子，他应史泰龙邀请去美国出演《敢死队》，带着 10人的保镖、按摩师、助手的豪华阵容，享受巴西阳光海滩，7天只工作3小时，片酬近亿，让人觉得这与公益的生活离开有点远。<br />
<br />
被问及为什么自己住着豪宅，却让别人来捐钱，李连杰引述达沃斯创始人施瓦布的话说：“世界上有两种模式，一种是德兰修女，用她无私的爱的奉献去传递爱；另一种是比尔盖茨，他用他的财富杠杆，以最大的量化去帮助人，而壹基金是采用了他们两个人中间的一种模式。”<br />
