今天早上，我接到了福建省的一个咨询电话，打电话的是一位女士。她告诉我，昨晚十一时许，十几个公安（有的穿了警服，有的没有穿）带着她老公到了家中，警察在没有出示搜查证情形下，拿走了一台笔记本电脑，她老公随即也被带走。 她在电话中说，自己老公名叫游精佑。<br />
<br />
&nbsp; &nbsp; 游先生妻子说，不知公安为何要抓她老公？我对她说，公安没有给拘留通知书，可能只是先传唤游先生。如果两天后还不放他出来，则是被公安机关拘留了。到时，公安机关会把拘留通知书发给家属。据我分析，游先生会是凶多吉少。<br />
<br />
&nbsp; &nbsp;据传，福建省几个网民的被抓是与严晓玲案有关。（注：以下六个自然段内容，摘自6月29日《法制网》的相关报道）<br />
<br />
&nbsp; &nbsp;６月２３日晚间起，新浪、网易、天涯社区、凤凰、中华网等多家知名网站论坛出现《严晓玲，她比邓玉娇悲惨一万倍！》、《惨绝人寰：闽清常现无名女尸　严晓玲被８人轮奸》等帖文，引起网民广泛关注。<br />
<br />
&nbsp; &nbsp; 文章称，位于闽清县医院对面的县文化馆里的“丽歌”ＫＴＶ，系闽清县公安局副局长林宗颖与治安科科长卢贤丁、县检察院涂检察官，伙同一个多次犯案且多次逃脱打击的黑社会头目聂志雄开办的。这家娱乐场所以贩卖Ｋ粉、介绍卖淫、胁迫卖淫、收取提成等为手段牟取暴利。<br />
<br />
&nbsp; &nbsp;文章中，林秀英叙述了女儿严晓玲长期被在闽清县有执法权力背景的聂志雄控制并被胁迫卖淫。２００８年２月１０日，严晓玲再次被聂志雄叫出去后一直未回家；２月１１日，林秀英在当地医院见到了女儿的尸体。在医院，林秀英听到了医护人员议论，“至少五六个以上轮奸，甚至死后还奸尸”，聂志雄父亲还向她透露，“严晓玲被８人轮奸致死”。<br />
<br />
&nbsp; &nbsp;文章描述了林秀英的见闻：公安机关声称要交５０００元才能做法医鉴定，警方将严晓玲子宫等器官割下以毁灭证据，掩盖事实等。最后，帖文公布了闽清县公安局局长刘伟光、县政法委书记陈继魁、县公安局办公室电话号码。<br />
<br />
&nbsp; &nbsp;文章被国内多家知名网站转载后，引起网民的广泛关注。６月２４日，百度网站上搜索到关于严晓玲的消息已经上万条，网民普遍表达了对严晓玲悲惨遭遇的同情和黑恶势力的愤怒。许多人还将矛头指向了当地党委政府，“丧尽天良”“警匪一家”等情绪化的表达形成汹涌的网络舆论风暴。<br />
<br />
&nbsp;&nbsp;２４日下午，福州市公安局紧急召开新闻发布会，政治部副主任刘明通报了严晓玲死亡事件详情，他指出网络上传闻是谣言。<br />
<br />
&nbsp;&nbsp;游先生的被抓，不知是否与严晓玲案的“传谣”有关？</div>

							
							
							
							
