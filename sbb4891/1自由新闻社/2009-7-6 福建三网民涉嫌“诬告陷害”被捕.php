<br />
　　今天上午，刘晓原律师向马尾区公安局求证何时批准会见，马尾区公安局要求三位律师下午到马尾区公安局来一趟。吴华英聘请的北京的金光鸿律师中午飞抵福州。于是，林忠、林洪楠、刘晓原和金光鸿四位律师下午一起去马尾区公安局。<br />
<br />
