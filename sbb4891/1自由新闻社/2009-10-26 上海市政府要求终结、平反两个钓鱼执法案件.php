													<h2>孙中界获平反</h2>
						
						
																											<div id="postmessage_300952" class="t_msgfont">孙中界知悉终结钓鱼执法消息后当场落泪(图)<br />
<a href="http://www.sina.com.cn" target="_blank">http://www.sina.com.cn</a>&nbsp; &nbsp;2009年10月26日13:10&nbsp; &nbsp;中国广播网 <br />
 <br />
此主题相关图片如下：szj.jpg<br />
<br />
 <br />
　　万众瞩目的上海钓鱼断指案新一轮调查昨天终于有了确定性的结论：就是钓鱼执法。<br />
<br />
　　新华社昨天发出电稿《勇于纠错取信于民》，作者是新华社上海分社社长兼总编辑慎海雄。这篇被网友认为最能代表上海市委态度的文章中披露，上海市委主要领导明确指出，要坚决取消不正当的执法方式，立即纠正这种执法错误。“上海市委、市人大、市政府、市政协的领导同志都作出了回应，使真相一步步大白于天下。”<br />
<br />
　　浦东新区调查认为，新区城市管理行政执法局在10月20日发布的所谓调查结论“与事实不符，误导了公众和舆论，损害了上海和浦东的形象”。<br />
<br />
　　二次调查进展神速<br />
<br />
　　上海市浦东新区城市管理行政执法局20日公布的调查报告斩钉截铁地宣布，孙中界涉嫌非法营运行为，事实清楚，证据确凿，适用法律正确，取证手段并无不当，不存在所谓的“倒钩”执法问题。结论一出，舆论风暴骤起，被称为“侮辱网民的智商”。<br />
<br />
　　由于上海市高层和社会各界的不满，浦东新区在报告发布的20日当晚又组建了由人大代表、政协委员、媒体记者组成的新调查组，展开新的一轮调查。与华南虎事件中二次鉴定千呼万唤不出来相比，这次可谓反应神速。<br />
<br />
　　新民网昨天透露，调查报告很可能今天发布。不过，调查结论昨晚已被新华社明确地披露了出来。联系到早前本报了解到的情况，新调查组中唯一的中央媒体代表就是新华社记者，这个信息被普遍认为是将要发布的调查结论。<br />
<br />
　　新华社：浦东执法局欲盖弥彰<br />
<br />
　　除此以外，昨天新华社还发了一条分量很重的述评《勇于纠错取信于民》，作者是新华社上海分社社长兼总编辑慎海雄。文章透露，日前，上海市委主要领导明确指出，要坚决取消不正当的执法方式，立即纠正这种执法错误；一定要本着有错就改、有错必纠的原则，实事求是、高度透明地向社会公布调查结果。同时，浦东新区区委区政府也对“孙中界事件”组织了调查，认为新区城市管理行政执法局在10月20日发布的所谓调查结论是在未经深入调查、仔细核实的情况下简单草率做出的，与事实不符，误导了公众和舆论，损害了上海和浦东的形象。<br />
<br />
　　新华社评论中将钓鱼称为“见不得人的非法手段”，称“钓鱼”“倒钩”，不仅损害了法律法规的公正威严和执法部门的声誉，还损害了党委政府在人民群众心目中的形象，损害了一个地方的社会诚信和社会良知。以此类损招、黑招来执法，与依法治国理念背道而驰、格格不入。<br />
<br />
　　“执法过程中出现了问题和错误，并不可怕，可怕的是对错误的态度。是查明真相、有错必究，还是文过饰非、欲盖弥彰？是痛定思痛、举一反三，还是我行我素、重蹈覆辙？这对各级党委的执政能力建设，同样是考验。令人欣慰的是，倒钩事件发生后，上海市委、市人大、市政府、市政协的领导同志都作出了回应，使真相一步步大白于天下。” 本报记者 谭人玮<br />
<br />
　　各方说法<br />
<br />
　　孙中界、郝劲松：将申请国家赔偿<br />
<br />
　　孙中界的哥哥向本报记者透露，有关部门已经通知他们今天上午去谈个话，具体内容不得而知。孙中界的要求仍然没变：还我清白。<br />
<br />
　　给孙中界提供法律援助的郝劲松也听说了这个消息，他表示，如果确实是认定了存在钓鱼式执法，那么不仅要要求国家赔偿，还要举报“钩子”陈某某涉嫌与执法人员相勾结，用引诱的方式和虚拟的事实来骗取非法利益。执法部门也要对他们的非法执法行为进行公开的书面道歉。<br />
<br />
　　此外，郝劲松还表示，除了孙中界这一个个案之外，应该复查这三年以来所有的同类案件。郝劲松说，要查清楚有多少无辜车主被“屈打成招”，被无端迫害，给多少家庭带来灾难和长远的心理伤害。这些年收的罚款究竟有多少，怎么花的等等。今天上午他就跟孙中界一起去进行陈述和申辩。<br />
<br />
　　张军：希望结论不限于个案<br />
<br />
　　引爆钓鱼话题的张军在听到新结论的时候说，他也希望这个结论不局限于孙中界个案上，新的调查结果也让他对自己的官司感到谨慎乐观。<br />
<br />
　　网友：抢劫款赶快退还！<br />
<br />
　　消息马上就被转到了爱卡上海分会论坛，有网友提出“重要的是先把5000万抢劫款退还给受害人，并公开赔礼道歉。”“启动国家赔偿无商量！”“知法犯法，从重从快处理相关责任人。”也许，下一步人们最关心的就是：“承认错了，就意味着他们执法犯法了，应该怎么处理？”<br />
<br />
　　本报记者 谭人玮</div>

							
							
							
							
