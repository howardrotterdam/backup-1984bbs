													<h2>上海浦东将就“钓鱼执法”案向公众道歉</h2>
						
						
																											<div id="postmessage_300620" class="t_msgfont">新华网10月26日报道 记者26日从上海市政府常务会议获悉,浦东新区将终结孙中界“钓鱼”式执法案并向公众公开道歉;闵行区张晖事件执法取证不正当,区政府将依法撤销原处罚决定。<br />
<img src="http://pic.dbw.cn/0/02/91/41/2914165_092452.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<img src="http://pic.dbw.cn/0/02/91/41/2914166_887819.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<a href="http://news.163.com/09/1026/10/5MHUDI15000120GU.html" target="_blank">http://news.163.com/09/1026/10/5MHUDI15000120GU.html</a><br />
<br />
据新华网快讯，浦东新区将终结孙中界“钓鱼”式执法案并向公众公开道歉；闵行区张晖事件执法取证不正当，区政府将依法撤销原处罚决定。据记者了解，中午11时半，新区政府将召开新闻发布会，公布调查结果。<br />
<br />
　　孙中界可以协商国家赔偿<br />
<br />
　　上午10时，孙中界的代理律师郝劲松表示他还没有看到网上的消息，他与孙中界正去往浦东新区政府，参加新闻发布会，公布案件调查结果。不过他介绍，他们刚刚与浦东新区城管执法局党工委书计，办公室主任等进行了十分钟的会谈。对方表示，孙中界14号的事件经过浦东新区的联合调查，结论是交通执法大队采取了不正当的取证手段，浦东区政府责令有关部门中止调查程序，归还被调查车辆，同时向孙中界同志表示诚恳的道歉，孙中界由此产生的一切财产损失可以协商国家赔偿，对伤口的处理，也想听听我方意见。<br />
<br />
　　郝劲松表示，他的要求是上海市浦东新区城市管理行政执法局就原南汇区交通行政执法大队栽赃陷害执法一事在上海媒体，河南媒体，及中央媒体向孙中界先生公开承认错误，赔礼道歉，恢复孙中界先生名誉。由于原南汇区交通行政执法大队栽赃陷害，使得孙中界先生遭受巨大精神创伤，为证清白，孙中界不惜断指以证清白。我方向上海市浦东新区城市管理行政执法局要求精神损害赔偿及其它赔偿。此外，由于原南汇区交通行政执法大队违法行政非法扣留孙中界先生车辆，我方要求上海市浦东新区城市管理行政执法局对扣留车辆造成的损失进行赔偿。<br />
<br />
　　钓鱼两当事人心情平静<br />
<br />
　　“对个案的处理这个结论我是认可的，但是他们避开了‘钓鱼式执法’的称呼，用了个‘不正当的手段’，到底是怎么个不正当，我认为他们应该勇敢的承认。”郝劲松说。<br />
<br />
　　孙中界对记者表示，他目前的心情很平静，从之前新华视点发布了有利于他的调查结论，有“倒钓”的嫌疑，他就有现在这样的心里预期了，他非常感谢全国舆论对他的支持。<br />
<br />
　　而“闵行倒钓案”的当事人张晖(化名张军)对记者表示，作为个案，他很高兴，不过他希望所以的钓鱼执法案都能得到公正处理。“我认为法律应该公正，政府应该有公信，执法人员不用乱使用手里的公权力，破坏社会的和谐。”张晖说，“我不知道国家赔偿怎么申请，我就希望他们能向我道个歉。”<br />
<br />
