													<h2>被遗忘的深圳缔造者</h2>
						
						
																											<div id="postmessage_820810" class="t_msgfont">　　.<br />
<br />
<br />
.<br />
<br />
<br />
<br />
<br />
<br />
<br />
.<br />
..<br />
.<br />
<br />
　　<img src="http://img4.cache.netease.com/photo/0001/2010-09-07/900x600_6FVJTOLS3R710001.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　她们是“外来妹”，她们是美国《时代》周刊的年度人物亚军，她们创造了这个繁荣的都市，却只能选择群聚在偌大厂区的一隅。她们是被遗忘的深圳缔造者。<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://img4.cache.netease.com/photo/0001/2010-09-07/900x600_6FUITE683R710001.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　20世纪80年代中叶，随着深圳特区建设的推进和国家用工制度的松动，大量农村剩余劳动力涌向深圳，其中包括大量年轻女性。图为1987年深圳市在广东五华县招工，一名前来报名的少女由干部测量并登记身高。<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://img3.cache.netease.com/photo/0001/2010-09-07/900x600_6FUIVKB53R710001.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　1991年，一间20多平米的女工宿舍住了近8个人。两年前，外来务工者开始持续涌入深圳。当年，电视剧《外来妹》在中央电视台播出，轰动全国，该剧主要描述六个从穷山沟到广东打工的女性的命运，“外来妹”一词传遍中国，开始为人们广泛关注。<br />
<br />
<br />
<br />
<br />
　　<img src="http://img4.cache.netease.com/photo/0001/2010-09-07/900x600_6FUIVBFK3R710001.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　由于当时深圳吸纳了大量电子、服装、玩具、五金等劳动密集型加工行业，心细手巧、听话吃苦、便于管理的农村女孩广受欢迎。图为1996年，深圳一家劳动力市场外边，一名打工妹临时拍摄证件照。<br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://img4.cache.netease.com/photo/0001/2010-09-07/900x600_6FUJ051P3R710001.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　到1996年，深圳总人口达到345万人，本地户籍人口仅有88万，而在剩下的外来人口中有68%是女性，平均年龄只有23岁。图为1996年，广东平远，17岁的张桂珍通过了招工考试，即将远行350公里去深圳打工，母亲张莲凤在车站和女儿惜别。<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://img4.cache.netease.com/photo/0001/2010-09-07/900x600_6FUIVSSL3R710001.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　深圳的外资企业产品多远销北美，订单多且紧，很多工厂规定工人每天都要加班，星期日也不能休息，长时间连续超时加班让不少女工频繁病倒。图为1990年，深圳，流水线上的女工趁工作间隙揉眼休息。<br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://img3.cache.netease.com/photo/0001/2010-09-07/900x600_6FUIU3PH3R710001.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　由于深圳的密集型加工行业多偏重招收女工，境内多个工业区性别比例严重失调，宝安一家拥有205人的塑料手袋厂，甚至只有4名男性员工。1982年9月，深圳一家企业的女工宿舍大门紧闭，男女工人们隔空相望。<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://img4.cache.netease.com/photo/0001/2010-09-07/900x600_6FUITM1U3R710001.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　各方面的不稳定，加上失衡的性别比例以及个人恋爱期望值的上升，打工妹中的一大部分都会对前途感到迷惘。图为1993年，深圳皇岗工业区，一名女工在床上写家书。<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://img3.cache.netease.com/photo/0001/2010-09-07/900x600_6FUITTTB3R710001.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　而相比感情，更突出的是福利待遇的失衡。由于户籍制度改革滞后，打工妹不能与城市居民的正式工一样累计工龄，她们少有晋升，更享受不到公费医疗、退休保险、失业保险和住房分配等各种社会福利。图为1982年9月，深圳，因劳资纠纷，一群打工妹集体要求辞职。<br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://img3.cache.netease.com/photo/0001/2010-09-07/900x600_6FUIU9OQ3R710001.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　而与所有劳工相同，打工妹们经受着比福利缺失更为悲哀的欠薪问题。图为1982年9月，因老板潜逃而拿不到工资，一名来自江西的打工妹滞留在工厂。<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://img4.cache.netease.com/photo/0001/2010-09-07/900x600_6FUIT9223R710001.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　何群(音)展示残疾的右手。何群在深圳一家金属器件厂打工，在操作一台公交车大小的打孔机时受伤致残。<br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://img3.cache.netease.com/photo/0001/2010-09-07/900x600_6FUIV3PJ3R710001.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　在工厂里，管理者和技术工人一般是男性，而女工多在生产线上，外来妹们的职业上升空间有限，长相好一点的姑娘则有机会进入重要场合做礼仪小姐，这对她们而言已是某种程度的&quot;晋升&quot;。图为1993年，深圳一场签字仪式上的礼仪小姐。<br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://img3.cache.netease.com/photo/0001/2010-09-07/900x600_6FUIUQQ13R710001.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　而另一批外来妹则选择了“充电”之路，她们一下班就骑单车去各种培训机构上课。蛇口工业区培训中心创办于1981年，是深圳的第一所培训中心，后来发展成为了深圳大学。图为1988年6月，深圳蛇口，夜校下课后，几位女工仍围着培训中心的老师提问。<br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://img3.cache.netease.com/photo/0001/2010-09-07/900x600_6FUJ0D953R710001.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　在当时的户籍政策下，农民外出打工必须持有身份证、务工证、暂住证，打工妹们还要持有未婚证，否则会被以“三无人员”遣返回家。外来妹虽然为深圳的发展贡献了自己的青春岁月，却未曾被这个城市所包容。图为1996年，深圳，城里人的婚纱吸引了很多打工妹驻足观看。<br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://img4.cache.netease.com/photo/0001/2010-09-07/900x600_6FUIT0FJ3R710001.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　1991年的《外来妹》之后，这个人群继续被淹没在深圳高速的经济发展中，直到18年后的2009年，深圳一家光电科技公司的4名女工照片登上了美国《时代》周刊，“中国工人”作为群体获评《时代》当年的年度人物亚军。冲在经济年增速“保八”第一线的中国工人，以女性劳动者的形象，第一次出现在了世界面前。<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://img4.cache.netease.com/photo/0001/2010-09-07/900x600_6FUSNN143R710001.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　30年过去，面对城乡差别，新一代外来妹的遭遇依然是区别待遇、情感歧视，依然没有足够的福利保障。图为2005年5月11日，19岁的黄珍(音)从一千多公里外的湖北老家前来深圳找工作，公司提供的宿舍和学校差不多。<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://img3.cache.netease.com/photo/0001/2010-09-07/900x600_6FUIUIE53R710001.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　更快速的经济发展，更大型的企业，更多的外来妹，更繁重的工作，这一切带来的是更严重的精神问题。2010年，富士康连续发生12起员工跳楼事件，其中有4起是女工跳楼。图为2010年5月29日，几名富士康女工走过富士康龙华园区门前。<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://img3.cache.netease.com/photo/0001/2010-09-07/900x600_6FUSV16E3R710001.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　而无论她们遭遇怎样，她们都间或不断地从各种渠道听到一些成功者的故事，这些故事让外来妹们有理由坚持下去。图为2010年8月19日，深圳安子新家政公司的安子为家政服务人员讲解家政服务注意事项。从流水线女工到作家，从商海折戟到公司董事长，安子谈起自己的特区故事时说：“深圳是一片最活跃的天地，每个人都有做太阳的机会。”<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://img4.cache.netease.com/photo/0001/2010-09-07/900x600_6FUISJP83R710001.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　梦想之外，更多的外来妹们，选择过着平静的打工生活，一份感情、一件新衣、一次聚会，撑起了她们的一年又一年。在深圳，这个因她们而高速繁荣的城市里，他们却只能选择群聚在偌大厂区的一隅。图为2009年2月17日，深圳一名女工下班回家。2010年，深圳经济特区成立30周年，在此诞生的“打工妹”也走过了30个年头。“深圳”已经成为繁荣高效的代名词，而“打工妹”何时才能被赋予新的意义，她们是被遗忘的深圳缔造者。<br />
<br />
