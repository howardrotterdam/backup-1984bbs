<br />
<br />
<br />
　　Evan Ramstad/The Wall Street Journal 图为中朝边境上立着的一块保护边境安全的宣传牌。中国警方通常会将逃北者逮捕并遣返回国，而这些叛逃者回国后将面临严厉的惩罚。 <br />
<br />
　　<img src="http://cn.wsj.com/pictures/photo/North_Korea_Quiet_Gateway/17.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
