													<h2>从丹东边境眺望朝鲜</h2>
						
						
																											<div id="postmessage_823702" class="t_msgfont">　　<br />
<br />
　　朝鲜劳动党将于周六召开自1966年来第一次全体党代会。站在中朝边境，依稀可见朝鲜可能即将发生的重大变化──新领导人，新经济政策以及与外界的关系。 <br />
<br />
<br />
　　. <br />
<br />
<br />
<br />
<br />
<br />
<br />
　　. <br />
<br />
<br />
<br />
<br />
<br />
<br />
　　. <br />
<br />
　　每天清晨，中国司机开着满载货物的卡车通过一座横跨在鸭绿江上的单行道桥梁，驶入朝鲜边境城市新义州。 <br />
<br />
　　<img src="http://cn.wsj.com/pictures/photo/North_Korea_Quiet_Gateway/01.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　Evan Ramstad/The Wall Street Journal 自1948年朝鲜建国以来，独裁政府专政，只允许极少数人入境──能从朝鲜出境的人更少。左图中，朝鲜男子聚集在一艘停靠在岸边的渔船旁。 <br />
<br />
　　<img src="http://cn.wsj.com/pictures/photo/North_Korea_Quiet_Gateway/02.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　Associated Press 56岁的中国卡车司机王全贵定期运货到朝鲜。他说：“朝鲜让我想起上世纪60年代的中国，每个人身上都佩戴有他们领导人头像的徽章。”左图摄于周一，几名朝鲜人站在一艘小船上。 <br />
<br />
　　<img src="http://cn.wsj.com/pictures/photo/North_Korea_Quiet_Gateway/03.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　Evan Ramstad/The Wall Street Journal 自近20年前苏联解体以来，中国已经成为朝鲜最重要的政治和经济支助者。左图中，新义州的居民用绳子吊着木桶从河中打水。 <br />
<br />
　　<img src="http://cn.wsj.com/pictures/photo/North_Korea_Quiet_Gateway/04.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　Evan Ramstad/The Wall Street Journal 在2005年，中国加大了对朝鲜的出口并扩大了贸易顺差。外界观察家认为，中国对朝鲜的主要经济援助手段便是贸易顺差。因为，朝鲜没有可衡量的债务工具，也几乎没有能力缩小贸易逆差。左图中，一艘朝鲜的小船侧翻在河中。 <br />
<br />
　　<img src="http://cn.wsj.com/pictures/photo/North_Korea_Quiet_Gateway/05.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　Evan Ramstad/The Wall Street Journal 中国的边境城市丹东是罕有的朝鲜商人获许可以进入的地区。左图中，站在丹东的江边眺望，对岸便是朝鲜。 <br />
<br />
　　<img src="http://cn.wsj.com/pictures/photo/North_Korea_Quiet_Gateway/06.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　Evan Ramstad/The Wall Street Journal 但即使是在丹东，朝鲜人也结伴而行，很少与中国人交谈。朝鲜商贩行事小心，以避免回国后被指控做过有损朝鲜利益的行为。左图中，在丹东旧城的外围地区，一片完全开放的新开发地──丹东新城拔地而起。 <br />
<br />
　　<img src="http://cn.wsj.com/pictures/photo/North_Korea_Quiet_Gateway/07.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　Evan Ramstad/The Wall Street Journal 中国提议在丹东新城新建一座通往朝鲜的多车道大桥，但未得到朝鲜方面同意。 <br />
<br />
　　<img src="http://cn.wsj.com/pictures/photo/North_Korea_Quiet_Gateway/08.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　Evan Ramstad/The Wall Street Journal 虽然这座大桥只有单车道，汽车、卡车和火车都要从上面经过。但到目前为止，这条连接丹东和朝鲜新义州的大桥是朝鲜最热闹的门户。震耳的号声每天在这里响起一次，标志着大桥上车辆的行驶方向改变。朝鲜每年对外贸易量的三分之二靠这座大桥维系。 <br />
<br />
　　<img src="http://cn.wsj.com/pictures/photo/North_Korea_Quiet_Gateway/09.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　Evan Ramstad/The Wall Street Journal 中国的卡车司机表示，朝鲜规定他们必须在中午前入境，否则要缴纳400元人民币（如果不要收据，只需200元）。到了晚上大桥的通车方向要调过来，司机们必须在下午五点之前离境。图为鸭绿江大桥的中方一侧。 <br />
<br />
　　<img src="http://cn.wsj.com/pictures/photo/North_Korea_Quiet_Gateway/10.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　Evan Ramstad/The Wall Street Journal 除了号声外，这条连接丹东和朝鲜新义州的大桥出奇的安静。每天只有百余辆卡车和寥寥几列火车从上面通过。很多游客在平台上用望远镜观望朝鲜。有时候二、三十分钟也见不到一辆车在桥上。 <br />
<br />
　　<img src="http://cn.wsj.com/pictures/photo/North_Korea_Quiet_Gateway/11.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　Evan Ramstad/The Wall Street Journal 鸭绿江大桥旁还有一座朝鲜战争期间被美军炸弹破坏的桥梁。图中桥上的大屏幕正在播放朝鲜战争的纪录片。 <br />
<br />
　　<img src="http://cn.wsj.com/pictures/photo/North_Korea_Quiet_Gateway/12.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　Evan Ramstad/The Wall Street Journal 图为鸭绿江大桥丹东一侧桥上的游客，在他们身旁是当年炸弹在桥上留下的弹痕。朝鲜并未对大桥的朝方一侧进行重建。 <br />
<br />
　　<img src="http://cn.wsj.com/pictures/photo/North_Korea_Quiet_Gateway/13.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
