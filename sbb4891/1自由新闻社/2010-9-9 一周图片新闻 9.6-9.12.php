<br />
<br />
<br />
　　Evan Ramstad/The Wall Street Journal 一名游客在鸭绿江大桥中方一侧的一个纪念品小卖铺旁打手机。 <br />
<br />
　　<img src="http://cn.wsj.com/pictures/photo/North_Korea_Quiet_Gateway/14.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　Evan Ramstad/The Wall Street Journal 去往朝鲜最近的路就在丹东城外。在鸭绿江上有一个由朝方控制的小岛，紧靠着中国的一条高速公路。 <br />
<br />
　　<img src="http://cn.wsj.com/pictures/photo/North_Korea_Quiet_Gateway/15.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　Evan Ramstad/The Wall Street Journal 照片中远处是中方建起的一道铁丝网隔离带，近处是一块边界告示牌，要求中国百姓不要与界外人员交谈和交换物品。 <br />
<br />
　　<img src="http://cn.wsj.com/pictures/photo/North_Korea_Quiet_Gateway/16.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
