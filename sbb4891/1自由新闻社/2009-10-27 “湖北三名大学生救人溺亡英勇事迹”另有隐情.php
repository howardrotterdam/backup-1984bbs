标题：转 3个大学生死亡的内幕！这才是真相！――转自校内一沙市人 <br />
我生活在3个大学生死亡地方的江堤下的荆堤小区，直线距离100米内，在那块水域，有15年以上的游泳经验。每年夏天的暑假期基本上无一天例外的在那游泳。下雨也去，包括98年那年发洪水的时候也无例外的在那游。 <br />
<br />
我只是看见现在网络上的大量抨击沙市人的素质低，什么见死不救的观点，忍无可忍的发表这篇文章，以我15年的亲身经验给大家分析过程。我没亲眼所见，不过我见过起码数百起宝塔河区域这样的事情，或者说是类似的。绝无夸张。 <br />
<br />
长江水流方向是自西向东，宝塔河因为万寿宝塔的存在，所以在当年的长江改道，也没有完全的改去。现在这个地方就是类似一个独立的内河，而且因为长江的中心水流自西向东，碰见万寿宝塔的拦截就成了自东向西的流向，不懂的同学在草稿纸上画个图就能理解，图画完了认真点看不难发现，长江的主航线是自西向东，水文站（就是落水的那地方）岸边的水是自东向西，于是宝塔河的这块水域就是一个直径300米左右的大漩涡。而且事实确实如此，我下岸的时候以背对岸来说是从左到右的水流，一出宝塔河区域，水流就成了从右到左。所以这样的特殊水域，纵然是一个游泳高手，自己游可能不会发现那有多么困难，只要后来拉一个人起来，救人难度是很大的，而且以我个人20岁以前的经验，一旦被水冲到万寿宝塔下方，那就在那回不来了。必须听那些高手的指点，顺水出到长江航线，再往回游，不然，我就只能这么说上帝创造了你，他也准备收回你了。本人技术也许不够，所以20多年来，没一次能不顺水出去而直接回来（因为水太急） <br />
<br />
这是水流的区域，告诉大家，在这地方救人的危险性。 <br />
<br />
其次，说一下人为的因素，住在长江边上（主要是指宝塔河那里）的人都会在孩子小的时候这么教育“看见有人掉下去了千万别去救，不然你自己也要死”。看了这句话是不是觉得人性很残忍？不是的，这是事实，宝塔河边上每年死多少人大家知道吗？ 报纸，电视上？还是百度一下？去查下？ 20多对吗？告诉你们，我一个暑假平均每天都看见有人拉尸体上来，多的时候一天就看见拉出4，5具尸体.（一个救孩子的父亲，还有一个老奶奶带着2个孙子的尸体，以及一个从上游冲下来的，这是我看见的最多的一次）。所以，对那几个死亡20多的数据，看了笑一下也就算了。别当真。这一段是告诉大家，那段水域的死亡率有多高。 <br />
<br />
说了半天还没说重点，这么时候说重点来了。差不多在8年前。在这段水域来了一票现在大家口中所谓的“渔夫”。他们开始来的时候专门打鱼，后来发展成了打捞死人，没记错的话在00年左右的时候一个死人的价格大约在2500-3000左右，便宜的时候好象有过2000的样子。我能确定，他们不是沙市人。很简单口音不对，我和他们聊过。最开始只是单纯的拉死上来。只有2个小船。到后来，我在一次偶然的时候和冬泳队的那群职业帮子去救一个人，快速游出去的时候，那船也动了，我以为他们是准备等别人死了又来打捞的（那时候他们已经发展成了一个口号，“只捞死人，不救活人”），我们也没准备和他们说什么，毕竟社会没要求每个人都是雷锋，最多我个人看不下去而已。但是郁闷的是，我游出去的路线，与他们的船开的路线，居然很巧合的会在某一点重合。于是我不得不绕一圈，先顺水后逆水的绕过去。等我游到那溺水人的身边时候我的体力基本消耗2/3，剩下的体力要连自己带那个人一起拉回去，我一游到那边上就把游泳圈丢过去（我去长江无论什么时候都有一个用摩托车轮胎内胎做的游泳圈，且用麻木绳系在身上，不离身），那个溺水的人估计是看见我了，伸手就抓我，瞎抓了2下，马上一手扣死我的颈部，直把我搞得呛一口水几乎见上帝。最后靠着游泳圈，我还是活着回来，不然今年就是我的2周年祭日。可能大家会更早的听说我这个名字，也成了“英雄”。 <br />
<br />
事后总结2条，第一，那票“渔民”的数学和流体力学真他妈学的好，真可谓一挡一个准。怎么没进中国科学院，真他妈的可惜。第二，以后再不搞这样的蠢事，算是上帝可怜我，没把我拉去陪他老人家。我实在没把握下次我出水救人还能活着见到第2天太阳，因为我救的人才14岁都有这么大的力气对我实施那“毁灭”性的一抓，真有后怕。 <br />
<br />
所以希望那些说沙市人都不见义勇为者自己心里有个底.不是没人去救的。如果真没人救，那里的死亡人数，我个人的保守估计起码翻一翻。 <br />
<br />
最重要的一点就是那些所谓的发的帖子说的“黑幕”的那一些废话，我给你们说下什么叫真正的“黑幕” <br />
<br />
在人落水之后，“渔民”会马上活动起来，他们的船就开始发动，然后会在他们估计的路线上阻挡你的营救――是不是很像间接的故意杀人？可惜事实就是这样。 <br />
<br />
然后当溺水的人沉下去之后他们马上会靠近那人，用网将那个人钩住（有的时候钩不住的那就没办法了），这个溺水的人就会被拖在船的后面，这时候溺水的人纵然清醒，也会在铁钩和网（那可不是一张小网，很有点重量的）的重量下，无法把头露出水面，即使没有清醒，从医学角度来说，溺水这么点时间也是能救活了。这就构成了谋杀。可惜啊取证太难。无法启动司法程序。等亲属去要求他们打捞尸体的时候，他们就会只专心谈价格，因为尸体的打捞他们不用关心，就在他们身后嘛.可惜你知道吗？不知道。你就必须接受那个价格。等价格谈好了，他们就开船绕一圈..然后回来，告诉你，人捞到了，该给钱了。最后看见的就是一个真正的死人了，无论是真死的，还是活着被“拖”死的。这才是真正的黑幕。这也是为什么这么多人却救不到几个人人，也是为什么一个3分钟就能完成的营救，却经常弄到15分钟之后还不能完成的原因。最后还弄个救的人死了，运气不好，救的人和被救的人都死了的结果。 <br />
