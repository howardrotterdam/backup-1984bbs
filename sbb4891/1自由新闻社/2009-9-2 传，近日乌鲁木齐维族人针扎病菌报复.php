<br />
　　朱海仑说，这些案件发生地点分布广，事发突然，犯罪嫌疑人流动作案，手段隐蔽，受害群众多是在事后察觉报案，案件侦破难度较大。经过公安机关的不懈努力，目前已抓获犯罪嫌疑人15人，其中刑事拘留6人，批准逮捕4人，目前案件正在全力深入侦查中。<br />
<br />
　　经医院接诊检查，目前没有发现一例劣性传染病病原体和有毒化学物导致的相关症状，没有一例就诊者因针刺伤害导致其他疾病住院，没有一例死亡者，也没有要使用抗病毒药物预防性治疗的病例。</div>

							
							
							
							
