													<h2>传，央视春晚门票一张一万</h2>
						
						
																											<div id="postmessage_467693" class="t_msgfont">央视春晚，其魅力究竟是在赵本山，还是在防止和阻碍大家见到赵本山？春晚彩排期间，全国记者蜂拥至北京，所有记者等候在门口，要进入是需要天时地利人和的好运气的。本报记者连续5天在北京中央电视台西门口蹲守，从第一天的没有进场，到第3三天的顺利混进场内，以此为大家揭秘进入央视之难，难于上青天。<br />
<br />
　　黄牛票一张票最高卖到近三万<br />
<br />
　　彩排两点开始，观众一点候场，记者11点就到达央视西门外。一个小时过去，两个小时过去，观众络绎不绝地从四面八方聚集到西门口。当记者上前询问有没有多余的票时，“黄牛”也出现了。都说有票的地方就有黄牛，此话一点不假。黄牛的行事作风可比小心翼翼的记者利落多了，见记者正与一个中年妇女沟通要购买其手中的票时，黄牛在一旁直接喊价，“400两张，您给我吧。”之后发现该阿姨在央视里面有路子，号称之后几场彩排和春晚当天的票她只要想都能拿到，黄牛一下来了兴趣，“阿姨，给您我的电话吧，您之后的票我都包了。”黄牛直截了当表示，有赵本山的那场彩排他可以出到500元一张，有王菲的那场1000元一张也成。<br />
<br />
　　更令记者大跌眼镜的是，黄牛对于大年三十当晚的春节联欢晚会给出的价格是――一万，“如果您有春晚当天的，一万一张卖给我。”收票都要一万元，这再一倒卖出去，价格可想而知。记者随后向该黄牛询问，他透露，自己每年这个时间都会如期出现在此收票，而印象最深的是有一年春晚当晚的票价卖到接近3万元人民币一张，连他自己都感到很不可思议，“春晚的票就是这么值钱，不怕卖不掉。”如此实力强劲的黄牛，也让试图从观众手中买得多余票的记者又多了个“劲敌”。<br />
<br />
　　四道安检一掏手机就被逮<br />
<br />
　　要进央视的大门，实在困难重重。要么通过春晚导演组的熟人直接拿到入场票，要不就是在央视门口以高价买观众票。其实，要进入西门还是有其他办法，比如请央视的熟人开个条带进去。但是，这第一道门进入的只是央视，而要进入演播厅场地范围，还要再过一道安检。此时保安便会逐一检查门票以及各种“进门条”的发放，一些无效或者过期的“不规则”票据会被拒之门外。<br />
<br />
　　而到了第三道检查时，基本外套和随身携带的包包都需要寄存，不允许任何摄影器材带入演播厅，检查非常严格，有个仪器会在全身扫一遍。记者了解到，有记者曾经把卡片机藏在靴子里、内衣里等等，但被发现的概率还是非常高。冲破“三重门”后，还只是能在后台等处晃悠。真正要进入一号演播厅，还需要最后一道安检。而这道检查时，只有票是有效的，其他任何证件都不能通行。<br />
<br />
　　本报记者当日总算过了重重安检，进入了一号演播厅，看到了春晚的带观众联排，然而，如果你存着侥幸心理，想要把手机拿出来拍照，那这如意算盘可是打错了。只见身边的观众刚掏出手机看短信，便有一个绿色的激光小点照着他，意在提醒其别想用手机拍照，虽然身边这位观众只是看短信而已。记者这才发现，在观众席的前后左右四周，都有严密的保安站岗，就是为了防止彩排情况被偷拍外泄。林艳雯 本报北京专电</div>

							
							
							
							
