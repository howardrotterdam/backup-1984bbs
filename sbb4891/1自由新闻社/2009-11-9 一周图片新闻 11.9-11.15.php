　　The south polar region of Mars is covered every year by a layer of carbon dioxide ice. In a region called the &quot;cryptic terrain,&quot; the ice is translucent and sunlight can penetrate through the ice to warm the surface below. The ice layer sublimates (evaporates) from the bottom. The dark fans of dust seen in this image come from the surface below the layer of ice, carried to the top by gas venting from below. The translucent ice is &quot;visible&quot; by virtue of the effect it has on the tone of the surface below, which would otherwise have the same color and reflectivity as the fans. Bright streaks in this image are fresh frost. More information . (NASA/JPL/University of Arizona)&nbsp;&nbsp;<br />
