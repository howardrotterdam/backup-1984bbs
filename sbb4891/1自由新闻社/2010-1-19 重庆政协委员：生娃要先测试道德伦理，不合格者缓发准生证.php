													<h2>重庆政协委员：生娃要先测试道德伦理，不合格者缓发准生证</h2>
						
						
																											<div id="postmessage_439746" class="t_msgfont">今日市政协会议开幕 委员提案：生娃前先测<br />
<a href="http://cq.qq.com/a/20100119/000019_1.htm" target="_blank">http://cq.qq.com/a/20100119/000019_1.htm</a><br />
<a href="http://cq.QQ.com" target="_blank">http://cq.QQ.com</a>　 2010年01月19日07:37 　 华商网-重庆时报　 　<br />
<br />
家长篇<br />
<br />
（记者伍文棠董茜）今日上午，市政协三届三次会议开幕。昨日，在最新收到的政协委员提案中，记者发现了这样一个提案――《由“星光宝贝计划”想到的》，这是市政协委员王小波的建议――要教育好孩子，首先要培养出高素质的家长。<br />
<br />
<br />
“蜗牛”反光标<br />
<br />
引发社会反思<br />
<br />
王小波说，他也当了父亲，教育和安全问题也一直是他最关注的问题。特别是在电视、报纸上看到很多青少年因家庭问题走上歪路，比如“孕跑跑”、17岁少年持刀抢银行等新闻，都让他很痛心。<br />
<br />
“现在学校都在加强对孩子们的道德教育，但当今社会，这已经不只是某个家长和某个学校的责任，而是维系到整个社会和谐发展的问题！”他告诉记者，自己就是看到重庆市教委、市公安局交警总队和重庆时报联手启动的“星光宝贝计划”活动，才联想到这点，不只是发放“蜗牛”反光标，还应该把整个社会整合成一股强大的教育力量。<br />
<br />
可请专家定期<br />
<br />
培训家长<br />
<br />
和普通人要求加强对孩子的教育不同，王小波认为，应该首先培养起高素质的家长。<br />
<br />
“家长因为自身素质的参差不齐，在教育孩子的过程中也会存在偏差和问题。”他提出，可以由工作单位、社区来牵头，邀请专家定期对家长进行培训，还可以组织家长进行教育的心得交流，并且组织专家到家庭中进行回访。<br />
<br />
考核不达标缓发证<br />
<br />
<strong>“在夫妻办理准生证的时候，就应该设置一个家庭观念、伦理道德的考核或考试，不达标就缓发准生证！”王小波告诉记者，这个并不是强硬的措施，主要是为促进家长主动学习，直到补考合格后就可以发证。</strong><br />
<br />
得知这一提案内容的市民对此褒贬不一，有人还对此争议了很久，认为有违相关法律精神。<br />
<br />
孩子篇<br />
<br />
建“4点钟学校”<br />
<br />
给孩子放学找去处<br />
<br />
（记者董茜伍文棠）孩子下午4点就放学了，此时还在上班的张晓惠不停地看墙上的钟：“孩子放学没有家长接送就如脱缰野马，安全如何保证？”对于这个难题，市政协委员马荣辉提出在社区建立“4点钟学校”的提案，呼吁街道、学校、社区三方规划协调，给孩子一个放学后的归处。<br />
<br />
小学门口爷孙一起“放学”<br />
<br />
家长上班无法接孩子放学，他们又是用何种方式来解决这一“时间差”矛盾？昨日下午4点左右，沙区树人小学门口30多位清一色爷爷奶奶辈老人，早早在校门口张望。70多岁的刘国富已经接到孙子，却拎着书包依然在等待。半小时后，刘国富背着2个书包，步履缓慢地跟在4个孩子身后，不时大喊：“慢点，慢点，注意车子！”<br />
<br />
“除了接自己的孙子下课，我还要负责将邻居的孩子接回来。”气喘吁吁的刘国富说，自从他答应帮一个三口之家的邻居接孩子放学后，他的“包袱”越来越重，短短3个月他就接到了3个邻居孩子放学的重任。<br />
<br />
张晓惠在南岸上班，孩子在沙区上学，她说：“家中没有老人，又找不到人帮忙接送。”孩子挂着钥匙放学就在外边游荡。她曾专门找到老师，希望能让孩子放学后参加体育队，实在不行就请老师安排孩子留下来做清洁，这样都要让她省心些。<br />
<br />
社区可建“4点钟学校”<br />
<br />
如果能建一所临时学校，有老师负责接孩子放学辅导功课，还有很多孩子一起玩游戏，家长对孩子安全问题就放心多了。马荣辉在提案中建议，建社区“4点钟学校”。社区居委会为管理主体，腾出专用房子，聘请退休教师对孩子进行作业辅导，提供一些娱乐项目。<br />
<br />
要缴纳一定费用<br />
<br />
当然“4点钟学校”不以盈利为目的，只要家长自愿报名，缴纳一定的管理费用，由市教委根据学校课后管理服务项目进行审查，测算成本后报物价部门备案就可以了。马荣辉说，收取的费用主要用于老师和管理人员的工资。</div>

							
							
							
							
