													<h2>《人民日报》连发三文探讨“五毛党”</h2>
						
						
																											<div id="postmessage_669456" class="t_msgfont"><a href="http://it.sohu.com/20100609/n272674190.shtml" target="_blank">http://it.sohu.com/20100609/n272674190.shtml</a><br />
<br />
【中国企业家网】近日，人民日报已罕见力度连发三篇文章探讨“五毛党”、水军的问题，这三篇报道的标题分别为《网络推手大起底：三道工序“炒”出网络红人》、《网络推手公司已达数千家 “网誉”市场渐成形》、《发帖公司像包工队》。向来“不苟言笑”的人民日报也开始探讨“五毛党”这样的新兴事物，看来网络生活已经渗透到社会的每个角落。<br />
　　以下为报道之一：<br />
　　“‘网络打手’招聘。薪资待遇：每个回帖：0.1―0.4元，每篇原创文章：5―10元，工资每日支付;要求：上网熟练，平均每天工作投入约1―3小时，具体根据效率自定，熟悉论坛操作……”<br />
　　这种公然招聘“网络打手”的信息分散在网站的各个角落，有的挂在论坛，有的挂在私人博客，有的挂在微博中，也有的在QQ群内转发。<br />
　　如今，互联网正成为恶意攻击竞争对手的一个工具。“网络黑社会”、“网络打手”、“网络刀客”等称谓，也开始出现。<br />
　　“打手”出没<br />
　　“一夜之间就可以形成强大舆论压力”<br />
　　去年3月10日，一篇名为《姐妹们小心了，揭露新东方老师的真面目》的帖子挂在凤凰网论坛。48小时后，该帖竟被4600多个论坛转载。帖子中称北京新东方学校老师欺骗女生感情，伪造高分成绩，并点出了老师名字的缩写。顿时，新东方陷入一场舆论危机。而事后查明，这是一种被“黑”。<br />
　　和新东方的遭遇类似，某汽车品牌也遭遇过“爬坡门”事件，杀毒软件商之间频起口水仗……诸多事件背后，“网络打手”的身影依稀可见。<br />
　　“一夜之间就可以形成强大舆论压力。被攻击的对象损失不小。”业内人士介绍，由于很难掌握确切的证据，“，“网络打手”们往往无需承担任何责任。<br />
　　谈到“网络打手”的由来，商都网新闻总监杨延方认为：“在一些热点事件中，有些人从网民的反应中看到商机，发现可以用利益组织网友有目的地发言，进而改变舆论的原生态，产生很强的媒体效果。”<br />
　　一位有着3年“从业经验”的业内人士告诉记者，很多“网络打手”公司的负责人都是早年在各大论坛比较活跃的网友，因为介入一些热点事件，获得了媒体关注，才创办“网络打手”公司。然而，这一行业内存在着严重的“收入差距”，一位“网络打手”就倒苦水说：“我已经干了两个月了，收入完全没有他们说的一天百元那么高，有时干了白干。”<br />
　　与“网络打手”伴生的，便是网络删帖公司――当客户出现负面信息时，为企业消除这些不利言论、删帖“消灾”。<br />
　　据业内人士介绍，如果有确凿证据表明是虚假诬陷，往往与网站或论坛正常接触就可以达到无偿删除。“如果是确凿的负面消息，申请删除就需支付费用”。<br />
　　而删帖，“除在网站或论坛删帖外，还包括在搜索引擎的删除。搜索引擎中都找不到相应链接，才叫删干净。”该人士介绍。<br />
　　常用的有几种办法，一种是直接删帖，一种是沉帖。前者主要是靠与论坛版主等具有删帖权限的管理者的关系，再支付一定的费用即可，不过各家删帖公司并没有统一报价，删掉一条信息，从几百元到上万元不等。上述人士称，据他了解，此前爆出的三鹿奶粉对某一搜索网站的“公关”事件，费用据说高达300万元。<br />
　　“沉帖”是通过论坛或搜索引擎的关系使其将相关帖子后移，“挪到十几页后，网民就没耐心向后点击了。”<br />
　　假若与网站的“公关”失败，删帖公司也可自行操作，“用大量的正面信息来冲”，把对客户不利的负面帖压下去。<br />
　　随着媒体的曝光，相关网站和论坛对删帖、删稿的管理加强了内部监控。但尽管如此，有业内人士在接受记者采访时仍直言，“有的公司可能删不掉，但我们肯定能。”<br />
　　“水军”秘密<br />
　　“发帖公司像包工队，包工头等于水军头”<br />
　　“网络打手”有专职和兼职之分。供职于某公关公司的资深策划人刘星透露，他们每炒作一个负面热点事件，需要成百上千的帖文同时出现在各大论坛。只有形成发帖的集群效应，才能轰动网络，进而引着普通网友不自觉地介入到已经策划好的炒作方向之中。而为了形成发帖的集群效应，就必须招募大量的兼职人员，业内称之为网络水军。<br />
　　刘星进一步告诉记者，“网络打手”发帖和回帖的内容都是制作好的。比如攻击一家房地产企业，帖子内容就要照顾网民对房价不满的情绪，攻击一家食品企业，帖子内容就要渗入大量网民非常关心的食品安全问题，只有迎合和调动网民的兴奋点，帖子才容易火起来，事件才容易攻击成功。水军回帖，也是由专职策划人员事先规定好注意事项，发给水军。接到发帖指令后，水军们会根据自己的任务进入规定好的网络论坛，并在各个论坛发帖。<br />
　　“水军是公开的秘密。手中没有几千水军，你都不好意思做这一行。”有业内人士如是表示。<br />
　　“发帖公司像包工队，包工头等于水军头，接单分发，赚差价。” 网络推手陈墨说。<br />
　　当然，水军不只接受打手任务，“黑”企业、“黑”产品，也有“抬轿子”，作“托”的。本质都一样――“有偿服务”。<br />
　　据介绍，网络水军的从业人员主要是在校学生、网吧的无业青年及一些小白领等，他们通过QQ、MSN等联系，一个水军头往往联系着几百名至几千名水军不等，需要联系时，“浪兄”等会直接联系水军头，布置任务，再由水军头转包给分散的水军，完成任务后，将发帖链接发回公司，按数量和质量发钱，通过支付宝、电子账号等分发到各水军手中，一般都不会直接见面。每条的价钱，按照质量，从一毛至一元不等。<br />
　　“浪兄”还介绍，为保证水军帖子质量，公司通常会规定，如回帖必须与某内容相关，要20个字以上等。同时还要求，水手要将自己发布的帖子的链接交给水军头，水军头汇总后发给公司，24个小时后抽查，一旦被删就不支付钱，所以发帖者必须不停顶帖。<br />
　　水军通常的“战场”在哪里?据介绍，主要在电子及日化、食品等快速消费品领域。<br />
　　不过最近，另一种利用网络操作信息的现象正在出现――用网络推手及水军发布虚假信息拉升股票伺机获利，“他们需要网络来配合炒作，如发布谁被收购、重组等消息。他们给的钱非常多，一个月服务费30万，成功后还有10%的提成。”网络推手“浪兄”就曾遇到类似情况。<br />
　　“水手”门槛<br />
　　“水手赚的也是辛苦钱”<br />
　　水手容易当吗?<br />
