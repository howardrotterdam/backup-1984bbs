													<h2>中国龙的未来</h2>
						
						
																											<div id="postmessage_442772" class="t_msgfont"><strong>　　以前刚好翻译了该书的一个评论，翻译全文见<a href="http://www.qiuin.com/2009/07/post-213.html" target="_blank">http://www.qiuin.com/2009/07/post-213.html</a>。 </strong><br />
<br />
<br />
　　------------------------------------------------------------------------------------ <br />
　　本文翻译自Economists.com,原文在此<a href="http://www.economist.com/books/displaystory.cfm?story_id=13983240" target="_blank">http://www.economist.com/books/displaystory.cfm?story_id=13983240</a>。原文标题Enter the dragon，应来自李小龙《龙争虎斗》的英文名称。 <br />
　　另：本人对书评原文作者以及原书作者的观点持保留态度。 <br />
　　------------------------------------------------------------------------------- <br />
　　 <br />
　　西方期望富裕、全球化和政治整合能让中国转性为一位温和的巨人。有新著认为这不过是幻想。 <br />
　　 <br />
　　 <br />
　　 <br />
　　很多竞争者争抢美国头顶上那枚全球最大权力的王冠。1950年代的苏联威胁着美国的军事霸权；1980年代的日本挑战美国的经济权力；而今的觊觎者是中国。美国衰落的证据看起来很明显：在入侵和占领伊拉克后就暴露了其军事力量的局限，始于华尔街的全球金融危机则显示了其资本主义体系的缺陷。西方现在指望中国能来支撑他们的金融体系，指望中国消费者能刺激全球经济的振兴。 <br />
　　先是由欧洲随后由美国等西方国家所长期占据的支配地位，最后在走向终结吗？英国时事评论员，近来作为中国、日本和新加坡等大学访问学者的马丁・雅克，他的答案很明确，其书名已说出了全部：&quot;当中国统治世界&quot;。 <br />
　　他首先引用了古德曼・萨奇最新的一项研究，该项目指出中国经济体将会在2027年超于美国，到2050年大约将是两倍于美国经济体的规模（而其时单个中国人仍将比美国人穷）。雅克从作为政治、军事和文化基础的经济权力出发，描绘了一个中国独霸全球的景象（a world under a Pax Sinica）。人民币将代替美元成为全球的储备货币，上海将遮盖纽约和伦敦金融中心的光彩；欧洲沦落为拥有辉煌历史的古代遗迹，很像今天的雅典和罗马；全球使用普通话的人如果不多于也至少等于使用英语的人；孔夫子的思想将变得和柏拉图的思想一样广泛传播；如此等等。 <br />
　　好像我们完全被带进了一场饶有兴趣的想象游戏。其实，雅克在政治和经济方面某些推断完全正确。作者不允许有不确定、混乱和错误出现。他预测历史将把中国在中古时期所拥有的世界权力地位归还给它，但这是否等于把中国拉回到如同大跃进和文革时期那种自我毁灭式的动荡状态呢？毕竟还是同一个共产党在掌权，并且如雅克指出的----中国政府绝不会和任何人分享权力。作者没有给予中国成千上万的抗议予以足够的重视，这些抗议大部分是反对腐败和失地。在作者浩翰详实的数据中仅有一处提及中国面临的人口问题，人口问题意味着中国或许在变富之前就已先完全变老。作者认为西藏和新疆的民族问题几乎没有不稳定的危险。 <br />
　　作为已停刊的英国杂志《今日马克思主义》的最后一任编辑，雅克认为共产党是一股温和的力量，领导中国走向繁荣富强从而避免苏联那样的垮台遭遇。他很少贩卖这样的观念：认为自由市场在长期看来，只能在自由社会运转；认为思想自由更有利于发明创新；认为民主国家会比威权国家更容易自我纠错。 <br />
　　雅克认为，这些不过是西方的自以为是。民主和法律规则并非是西方经济实力的先决条件，而只是一个巧合。这种观点是雅克全书最有趣（也是最富争议）的部分，远胜过那些关于中国历史枯燥乏味的叙述，或者那些关于中国将成为一个&quot;文明国家&quot;而非&quot;民族国家&quot;的冗长论述。 <br />
　　依据作者的观点，欧洲与中国分道扬镳并非始于文艺复兴或启蒙运动，而是工业革命。即便如此，西方的成功也并非注定的。雅克认为，直到1800年，中国还能与欧洲在最先进的领域 保持相当的发展水平。事实上，中国在瓦特之前已经建造了某种形式上的蒸汽引擎。那么为什么工业革命始于英国而不是长江沿岸呢？主要还是历史的偶然因素。 <br />
　　英国和当时的中国一样遭遇了土地短缺。但英国有煤炭来替代木材作为燃料，并且有奴隶耕作的殖民地为英国提供了充足的耕地和廉价劳动力。战争天性&quot;把欧洲民族国家打造成了名副其实的战争机器&quot;，加之进入精英阶层的商人阶级联合起来鼓动欧洲统治者促进资本主义发展。雅克认为，相比之下，帝制时代的中国还维系着孔夫子的和谐价值观，统治者本意主要着眼于其治下的稳固秩序和社会平等。因此，西方并非以其优越的价值观念统治了世界，而是凭借它的缺陷。 <br />
　　 <br />
　　如果拓殖帮助西方建立了霸权，那么二战后殖民时代的终结为中国的崛起铺平了道路。1978年开始的中国经济发展是&quot;人类历史上最辉煌的奇迹&quot;，其速度远远超过欧洲或美国，也甚至快于日本、南韩和亚洲其他经济奇迹。雅克认为，伴随德国和日本崛起的某些冲突虽然无法排除，但还是有很好的时机来加以避免。&quot;中国并不期求统治世界因为他相信自己就是世界的中心。&quot;作者这样写道。现在的中国正致力于同西方合作，它依赖西方的投资和市场，也希望有稳定的海外环境。 <br />
　　西方期望富裕、全球化和政治整合能让中国转性为一位温和的巨人，一只熊猫而不是一条龙。1999年时老布什宣称：&quot;中国进行自由贸易的时间掌握在我们手上。&quot;然而，雅克说这是一个幻觉。时间不会使中国更西方，而使西方以及世界更中国。 <br />
　　《当中国统治世界：中央之国的崛起和西方世界的终结》马丁・雅克 Allen Lane出版 2009.6</div>

							
							
							
							
