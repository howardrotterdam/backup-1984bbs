　　・外文书名：When China Rules the World: The End of the Western World and the Rise of the Middle Kingdom<br />
<br />
【内容简介】<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;《当中国统治世界:中国的崛起和西方世界的衰落》讲述：“中国绝对不会走上西方民主化的道路，只会选择一条不同于西方世界的发展模式；中国的崛起将改变的不仅仅是世界经济格局，还将彻底动摇我们的思维和生活方式。”<br />
　　过去200多年的时间里，我们一直生活在西方制造的世界中，生活在一个“西方”概念等同于“现代”概念的时代里。<br />
　　马丁&#8226;雅克，一位学贯东西的全球顶级学者以深邃的目光透视了中国发展模式和西方模式的根本异同。他认为，21世纪将是一个完全不同的世纪，随着非西方国家力量的迅速兴起，西方将不再占据主导地位，一国实现现代化的方式也将多种多样。在这个充满“现代竞争性”的新时代里，中国将成为全球竞技场上的核心角色。中国经济的迅猛增长已经产生了深远的影响，并一直为国际社会津津乐道，但是其影响力远远不止这些：中国的崛起标志着西方民族国家在全球主导地位的终结，同时也表明一个运用多种不同方式塑造的新型世界的崛起。<br />
　　《当中国统治世界:中国的崛起和西方世界的衰落》极富远见性的著作中，作者敏锐地观察到，中国绝不会变成一个西方式社会，它将保持高度鲜明的本国特色；西方发达国家代表的现代化发展模式目前主导世界，但将受到中国所代表的“另类发展模式”的挑战，直到有一天“东风压倒西风”，中国模式主导世界；随着中国的崛起，西方将丧失文明操纵权，世界将按照中国概念重新塑造……<br />
　　马丁&#8226;雅克以让人极其信服的观点预测到，如果说英国曾是海上霸主，美国是空中和经济霸主，那么中国将成为文化霸主。中国的崛起将改变全球，世界将由西方塑造走向更多的中国塑造。<br />
　　《当中国统治世界:中国的崛起和西方世界的衰落》是一部还在创作阶段就已经轰动全球的书，一部瞬间让“中国崩溃论”、“历史终结论”变得如过眼云烟的书，一部预言中国正在改变世界轨道的书究竟拥有什么样的独特魅力，评判甚至挞伐的权利还是要让与亲爱的读者朋友。<br />
<br />
【作者简介】<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 马丁&#8226;雅克，学贯东西，中国人民大学访问学者。是LSE IDEAS的高级客座研究员，同时也是伦敦经济学院亚洲研究中心客座研究员。曾在京都立命馆大学、新加坡国立大学任教，创办英国智库Demos，亦有丰富的新闻从业经历，做过《泰晤士报》、《星期日泰晤士报》、《卫报》和《新政治家》的专栏作家，以及《独立报》副总编辑，为BBC撰写稿件并主持过多次电视节目。马丁&#8226;雅克与人合编和合著了多部作品。<br />
<br />
【媒体推荐】<br />
<br />
这是一部震惊了西方世界的书：书稿还在创作阶段就已经引起了西方媒体的一片沸腾，它们都在关心――究竟中国能不能统治世界？中国统治世界的力量是什么？这更是一部震惊了中国的书：在英文版出版后，作者马丁&#8226;雅克在中国的旋风之旅已然在各大媒体呈现遍地开花之势，赞美、反思甚至大加挞伐之声不绝于耳，而更多的读者则关心――这部书的中文简体版到底什么时候出版？<br />
　　――编者手记<br />
　　马丁&#8226;雅克是第一个研究中国崛起的思想影响的学者。他这本《当中国统治世界》的历史厚重性和未来前瞻性超越了我所读过的有关中国崛起的所有著作。本书暗示人们，中国崛起的结果将不是中国越来越像西方，而可能是世界越来越像中国。想展望世界未来的人需要读这本书。<br />
　　――阎学通(著名国际关系学者、清华大学国际关系学系主任、教授、博士生导师)<br />
　　2009年是世界大反思的一年,马丁&#8226;雅克所撰写的这本书也是世界大反思的反映。二十年前，当柏林墙倒塌的时候，西方一些人欣喜若狂，以为“历史终结了”，西方可以“稳坐天下”了，全世界都得按照“华盛顿共识”办。然而，世界的发展与变化与他们所预言的不一样，中国就是一个很突出的例子。马丁&#8226;雅克经过多年的研究和思考，写出的这本书，对中国人今天所走的这条路有独到看法，很值得一读。它会帮助我们更好地认识当今世界和我们自己。<br />
　　――吴建民(欧洲科学院院士、副院长)<br />
　　2009年12月22日<br />
　　马丁&#8226;雅克的学识、观察亚洲问题的视角和经历，在这本书中得到了淋漓尽致的体现，它给真正想要理解中国和预测未来几十年里世界政治走向的人提供了非常宝贵的帮助。雅克认为在一个充满“竞争的现代性”的新时代里，正在经历现代化同时已经实现现代化的中国，将会保持该国本质上的中国性。这一观点是难以反驳的。<br />
　　――时殷弘(著名国际关系学者、中国人民大学国际关系学院教授)<br />
　　为何中国会发展成现在的模样，它将会走向何方，这是两个永恒的问题。我赞成马丁&#8226;雅克的“文化是理解中国的关键”的观点。毫无疑问，这是我曾经读过的关于中国研究的最优秀最严肃的作品之一――它实在是太让人陶醉了。<br />
　　――余永定(中国社会科学院学部委员、世界经济与政治研究所前所长)<br />
　　这本书不只限于描写中国崛起，它还揭示出中国崛起对于正在出现的世界秩序和全球人民的生活方式所暗涵的寓意。马丁&#8226;雅克对这个问题有着深思熟虑的想法，这本富有前瞻性的著作为我们提供了看待未来的重要一瞥。<br />
　　――马凯硕(新加坡国立大学李光耀公共政策学院院长)<br />
　　中国的崛起，被证明是当今世界具有划时代意义的经济和地缘政治的变化，没有哪一位英国学者能够像马丁&#8226;雅克那样，对此既投入更深层次的主观思考，又给出极富启示性的分析。<br />
　　――尼尔&#8226;弗格森(哈佛大学教授、著名金融史研究专家)<br />
　　这是一本经过作者的深入思考、充满了对历史的解读而且现实主义色彩浓厚的重要书籍，它不仅仅是在描述中国，它更是在描述关于21世纪将不再被北大西洋力量、观念和假定所左右和塑造的著作。我推测它一定会产生巨大的影响。<br />
　　――艾瑞克&#8226;霍布斯鲍姆(享誉国际、备受推崇的近代史研究大师)<br />
