<br />
&nbsp; &nbsp; 12月20日《日本时报》载文《在圣诞节提醒朋友关注中国》，摘要如下：<br />
<br />
&nbsp; &nbsp; 最后一批圣诞节购物者请注意：我们有一款不难购买的礼品向您推荐。我们保证，这款礼品适合所有口味。前提是您打算赠送的人要聪明、有修养，渴望了解世界。答案便是为您的朋友购买一本关于中国的书--《当中国统治世界》。绝对物有所值。<br />
<br />
&nbsp; &nbsp; 中国人口占世界的20%，拥有美国债券比富裕的日本还多，消耗着日益增多的世界能源和矿产资源，正如好莱坞电影所言，中国现在是新的主要剧情。<br />
<br />
&nbsp; &nbsp; “中国是房间里的大象，大家明明看到了，却不愿去谈。”这是雅克令人津津乐道而咄咄逼人手法的完美例证。这种手法的结果是：一本颇有价值的书。<br />
<br />
&nbsp; &nbsp; 《当中国统治世界》中涵盖众多问题，得出的结论有力而令日本人不安。作者马丁&#8226;雅克认为，中国的崛起遵循自己的方式。这意味着中国的现代性将和西方的不一样。中国崛起之后，和西方民主也不甚相同。亚洲思想者这些年来一直告诉大家“中国人来了”。新加坡的思想者，特别是李光耀等，一直反覆告知世界注意中国。<br />
<br />
&nbsp; &nbsp; 的确，面对中国这头大象的崛起，日本人开始惊慌失措了。正如《当中国统治世界》一书中提到的那样：“日本对中国崛起的反应暴露出其本质上的弱点，日本就像一只兔子，在迎面而来的汽车的明亮车灯下显得有些惊慌失措。”</div>

							
							
							
							
