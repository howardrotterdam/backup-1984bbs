													<h2>《当中国统治世界》中文版上市</h2>
						
						
																											<div id="postmessage_439821" class="t_msgfont"><img src="http://images2.china.com/culture/zh_cn/reading/first/11022801/20100112/15773648_556422.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
&nbsp; &nbsp;&nbsp; &nbsp;<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;作者:(英国)马丁・雅克 译者:张莉 刘曲 <br />
　　・出版社：中信出版社<br />
　　・页码：380 页<br />
　　・出版日期：2010年01月<br />
　　・ISBN：9787508618326<br />
