																											<div id="postmessage_425407" class="t_msgfont">余秋雨铁凝韩寒等当选中小学生最喜爱当代作家<br />
<br />
2010年01月11日 09:25<br />
扬子晚报<br />
<br />
<img src="http://img.ifeng.com/hres/201001/11/09/d56bee527ef4391f87608430fe3b751c.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
余秋雨<br />
<br />
<img src="http://www.cnsqzj.cn/uploads/userup/0807/1416254A132.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
铁凝<br />
<br />
“孩子们给我们颁的奖，分量最重！”由《扬子晚报》和《全国优秀作文选》联合举办的首届“全国中小学生最喜爱的当代作家”颁奖活动昨天在南京大学科技馆大礼堂举行，在孩子们的掌声中，余秋雨、肖复兴、赵丽宏、秦文君、黄蓓佳和梁晓声六位“大腕”作家一一上台领奖(作家顺序按姓氏笔画排列)，随后他们与从各地赶来的学生代表及《扬子晚报》的小记者们进行了精彩的互动交流。因某些特殊原因无法现场领奖的史铁生、铁凝、曹文轩及韩寒则通过视频、题词等方式表达了获奖感受和对中小学生的勉励。<br />
<br />
本次活动得到了中共江苏省委常委、宣传部长杨新力的充分肯定，杨部长多次予以关心。江苏省作协主席王臻中、扬子晚报副总编辑周桐淦、江苏凤凰传媒集团副总经理孙真福、江苏教育出版社社长张胜勇及南京大学文学院院长丁帆昨到会颁奖。<br />
<br />
●评选揭晓<br />
<br />
他们十位是中小学生最喜欢的作家<br />
<br />
余秋雨:这个奖穿越了时空<br />
<br />
【作家介绍】借我一生文化苦旅行无疆，寻觅中华笛声何处叹千年。他用创造文化的历史而载入历史。文化散文、历史散文、文化美文……近十年的畅销书他独占四本。他用自己的努力唤起了更多的中国人对中国文化的温情和敬意。他的演讲倾倒了无数听众。他履行了一个文化普及传播者的神圣职责。<br />
<br />
【获奖感言】余秋雨此次专程来宁领奖，一领完奖就要赶飞机去北京。但对于这个奖余秋雨表现出极强的珍惜之情:“我虽然得过很多奖，但很多奖我不会去领，然而今天的这个奖我是十分看重的，因为它穿越了时空。据我所知，这个奖是由老师和学生们投票选出来的。老师的投票让我回到了童年时代，老师是给我文章第一个投票的人，因此老师们的支持代表了过去和现在，而同学们的投票对我来说就是‘明天’的投票，可见文学是穿越时空的，这个奖也代表着时空的概念。”对于热爱文学的青少年，余秋雨先生特别提出了自己的殷切希望:“我希望孩子们不要把文学看作职业，它代表着一种素养，具有这种素养的人能比其他人更有爱心，更敏感于善恶，更善于表达。我并不希望大家以后都来专业从事文学创作，而是希望大家可以成为拥有文学素养的律师、工程师、公务员等，这是一个‘过来人’给大家的意见。”<br />
<br />
肖复兴:文学让我们的心充满美感<br />
<br />
【作家介绍】他的优美散文入选大中小学课本。他的小说和报告文学，从《早恋》到《青春梦幻曲》，从《一个女中学生的日记》到《多梦时节》……他是曾经的中学教师。他一直在用教育的理性和文学的情感为中学生编织美丽的梦想。那情到深处的心理疏导，那视域宏阔的思想引导，那循循善诱的方法指导……他在塑造当代中学生形象时也留下了一个时代的青春风景。<br />
<br />
【获奖感言】对于此次获选“中小学生最喜爱的当代作家”，肖复兴先生感慨万千:“1963年我读初三，那时候在北京市组织的一次少年儿童征文比赛中获了奖，而就是这篇作文让我从此走上了文学的道路。因为这篇作文受到了叶圣陶先生的指导，他不仅亲自批改，而且还专门约请我到他家去交谈。与文学大师的亲密接触让我深切感受到了文学的魅力，从此我更加热爱文学。同学们，文学足以引导我们的一生，它不是让我们有飞黄腾达的机会，而是让我们的心充满美感。祝愿大家在热爱文学的道路上走得更仔细、更长远。”<br />
<br />
赵丽宏:以后我写作时会想着你们<br />
<br />
【作家介绍】打开初中语文第一册第一课，映入眼帘的就是他的散文名作《为你打开一扇门》。有人说，他就像一个幸福的老师，他的面前每天都有一幅灿烂的风景――千百万学子在课堂上深情朗读他的优美散文。他的30篇作品入选实验教材，60部集子成就文学人生。他用一生的努力终于推开了属于自己的文学之门。<br />
<br />
【获奖感言】“没有一次获奖像今天这样让我感到珍惜。”拿到奖杯的赵丽宏表示:“写作已经30年了，以前写作时并没有想到这些作品是专门给孩子们看的，但没想到，今天受到了这么多孩子的认可。谢谢你们的鼓励，以后我写作的时候心里面一定会想着你们！”<br />
<br />
秦文君:寒冷季节里最温暖的礼物<br />
<br />
【作家介绍】有爱就有力量。因为爱孩子她才用近30年的漫长时间为孩子奉献了400万字的优秀儿童文学作品。<br />
<br />
有爱就有成功。她塑造的一系列性格鲜明、生动活泼的人物早已定格为当代儿童文学的经典形象。<br />
<br />
【获奖感言】秦文君女士一直很有“学生缘”，对于这次获奖，秦文君女士称为“寒冷季节里最让人感动和温暖的礼物”。她勉励所有热爱文学的孩子们说: “对于一个真心特爱文学的人来说，一方面要多读好书，另一方面构建文学作品时还是要看人心灵深处的花草。我会努力为小读者们写出更好的作品来。”<br />
<br />
黄蓓佳:让我们享受文学带来的幸福<br />
<br />
【作家介绍】她是孩子们眼中的大明星，校园里的追捧者众多，她的新作总是被抢购一空；她用思想引领孩子感知世界的丰富多彩；她用深度启迪孩子发展的无限可能；她用敏锐的观察力和卓越的表达力传递“人人是尧舜，个个是人才”的现代教育观念；她用形象、用情感、用故事、用美把每个孩子导向自由发展的天地。<br />
<br />
【获奖感言】昨天的黄蓓佳女士身着玫红色小棉袄，十分喜庆，足见她此次获奖的心情:“我从1972年从事写作，得了很多奖，但今天这个奖因为是同学们发给我的，所以我很开心。你们是文学的希望，在很多成年人或在职场拼搏或在生活娱乐的时候，我很庆幸还有你们在如此认真地阅读文学。让我们一起享受文学带来的幸福吧！”<br />
<br />
梁晓声:像感谢父母一样感谢文学<br />
<br />
【作家介绍】他曾因一篇小说而上复旦大学已成为文学走向人生成功的经典故事。他以一批经典作品为一代知识青年树起的纪念碑――当悲壮伴着反思化为永恒，我们仍然能隐约听到“正义与善良”的遥远回响。他是作家，也是深受学生爱戴的大学老师。<br />
<br />
【获奖感言】“我有多感谢我的父母，就有多感谢文学。” 梁晓声先生对文学表达了深深的敬意:“我从小是在哈尔滨市一个很脏很乱的大杂院成长的，那时的生活很贫穷，父亲又在遥远的南方工作。我有很多种可能成为街头巷尾的那种小混混，但是我没有，是什么引导我的生长呢，那就是文学。文学让我知道该做什么，不该做什么。要相信，文学是可以影响人的情感，影响人性的。”<br />
<br />
史铁生:牢记诚实，善于思考，热爱过程<br />
<br />
【作家介绍】史铁生残缺的身体里闪射出的是最健全最绚丽的生命光芒。他用文学超越一己之苦难，又以苦难点亮他人的心灵之灯。在高度技术化和物质化的时代，史铁生的生命之美正继续悄无声息地安抚、塑造着许多人的灵魂。<br />
<br />
【获奖感言】因为身体原因而无法出席活动的史铁生先生对于此次获奖十分高兴，颁奖会上特别展示了他为勉励文学少年而作的题词:“牢记诚实、善于思考、热爱过程。”<br />
<br />
铁凝:以后有孩子的活动一定参加<br />
<br />
【作家介绍】从中学时代的获奖作文《会飞的镰刀》到30年后的代表作品《永远有多远》，她在文学的梦幻里寻找永远。她是中国文坛的领军人物，不仅仅是她接替巴金成为中国作家协会主席，更因为她永远的卓越追求和源源不断的精品力作。她没有刻意用笔寻找自身的历史坐标，她只是用自己的心挽留住了直面人生的一片童贞。<br />
<br />
【获奖感言】由于有重要的会议，铁凝女士没有能亲临现场。但是她对获得“中小学生最喜爱的当代作家”称号很重视，并表示:“以后有孩子们的活动，一定参加。”<br />
<br />
曹文轩:文学值得我们每个人亲近<br />
<br />
【作家介绍】他是研究文学的大学教授，他是精品迭出的高产作家。《草房子》的第100次印刷，证明了一个不一样的儿童文学作家。他唤醒了多少家长对童年生活的美好回忆，他引起了多少孩子对未来世界的翩翩遐想。他为家长和孩子，也为老师和学生搭建了一座情感共鸣的心灵桥梁。<br />
<br />
【获奖感言】没有亲自来领奖，是因为曹文轩先生当天在北京召开《草房子》第100次印刷的新闻发布会，他是不能缺席的主角，但是曹文轩先生特别制作了视频表达了对孩子们的鼓励:“世界上有一些东西可以不去亲近，但是文学是值得我们每个人亲近的。因为它能让人高尚，让生活更有情调。”<br />
<br />
韩寒:要学到自己感兴趣的东西<br />
<br />
【作家介绍】中学时代的韩寒是学校的难点，他因不适应现有教育体制而退学。走向社会的韩寒是文坛的热点，一部《三重门》引发“韩寒现象”的讨论。赛车手的韩寒是媒体的焦点，荣获全国总冠军又劲爆“韩寒当市长”的网络热门话题。青春的韩寒更是全国青少年的兴奋点，十余部作品，超千万读者，以及博客写作 2.7亿次的海量浏览。<br />
<br />
【获奖感言】作为80后唯一入选的新生代作家，韩寒因为正在芬兰参赛而无法赶到南京领奖，但是获奖的韩寒特地录制了一段赛车时的视频，还小幽默了一把:“为了让你们看我看得更清楚一些，我先把窗帘拉下来。”韩寒笑着说:“你们选择我是没错的，证明你们很有眼光！我希望大家都能学到自己真正感兴趣的东西，训练好自己的本领搏击社会。最后祝大家新年快乐，寿比南山！”</div>

							
							
							
							
