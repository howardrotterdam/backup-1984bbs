<br />
　　1975年奉调至北京中国佛教协会。1980初，北京广济寺传戒任开堂。1981春被派往日本京都净土宗佛教大学晋修，考察日本佛教僧侣教育，1983年12月回国。1984年初，受任中国佛学院教务长。1986年任中国佛学院副院长。1991年8月至天台山下方广寺专修念佛，经三年。 1994年8月，受请任江西庐山东林寺住持。1995年初，蒙中国佛教协会调回中国佛学院。<br />
