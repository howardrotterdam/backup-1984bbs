<br />
2010-06-28 10:55:00　来源: 中国新闻网(北京)<br />
<br />
核心提示：中组部长王秦丰28日透露，截至2009年底，中国共产党党员总数为7799.5万名，比上年净增206.5万名。<br />
<br />
中新网6月28日报道 中共中央组织部副部长王秦丰今日介绍说，截至2009年底，中国共产党党员总数为7799.5万名，比上年净增206.5万名。中国共产党现有基层组织379.2万个，比上年增加7.4万个，其中基层党委18.3万个，总支部23.5万个，支部337.4万个。<br />
<br />
其中，女党员1694万名，占党员总数的21.7%。少数民族党员513万名，占党员总数的6.6%。具有大专以上学历的党员2787.3万名，占党员总数的35.7%。35岁以下的党员1847.3万名，占党员总数的23.7%；36岁至45岁的党员1687.6万名，占21.6%；46岁至59岁的党员2283.5万名，占29.3%；60岁以上的党员1981.1万名，占25.4%。<br />
<br />
工人693.7万名，农牧渔民2402万名，党政机关工作人员659.6万名，企事业单位管理人员、专业技术人员1772.5万名，学生226.9万名，离退休人员1452.5万名,其他职业人员592.3万名。<br />
<br />
据王秦丰介绍，全国共有党的各级地方委员会3226个，其中省(区、市)党委31个，市(地、州)党委400个，县(市、区)党委2795个。<br />
<br />
城市街道、乡镇、社区、建制村党组织。全国6629个城市街道建立了党组织，34224个乡镇建立了党组织，8万个社区(居委会)建立了党组织，59.8万个建制村建立了党组织。<br />
<br />
全国277万户企业中，具备建立党组织条件的64.4万户，其中64.2万户建立了党组织，占99.6%。23.4万户公有制企业中，20.4万户建立了党组织，占具备建立党组织条件企业总数的99.9%；253.6万户非公有制企业中，43.8万户建立了党组织，占具备建立党组织条件企业总数的99.5%。<br />
<br />
事业单位党组织。全国57万个事业单位中，47.1万个建立了党组织，占具备建立党组织条件事业单位总数的98.1%。其中1638所普通高等学校全部建立了党组织；7701个科研单位中，7546个建立了党组织，占具备建立党组织条件科研单位总数的99.8%。<br />
<br />
社会团体、民办非企业单位党组织。全国1.3万个具备建立党组织条件的社会团体中，1.2万个建立了党组织，占91.1%。全国1.6万个具备建立党组织条件的民办非企业单位中，1.5万个建立了党组织，占97%。（据人民网文字直播）<br />
