<br />
城市街道、乡镇、社区、建制村党组织。全国6629个城市街道建立了党组织，34224个乡镇建立了党组织，8万个社区(居委会)建立了党组织，59.8万个建制村建立了党组织。<br />
<br />
全国277万户企业中，具备建立党组织条件的64.4万户，其中64.2万户建立了党组织，占99.6%。23.4万户公有制企业中，20.4万户建立了党组织，占具备建立党组织条件企业总数的99.9%；253.6万户非公有制企业中，43.8万户建立了党组织，占具备建立党组织条件企业总数的99.5%。<br />
<br />
事业单位党组织。全国57万个事业单位中，47.1万个建立了党组织，占具备建立党组织条件事业单位总数的98.1%。其中1638所普通高等学校全部建立了党组织；7701个科研单位中，7546个建立了党组织，占具备建立党组织条件科研单位总数的99.8%。<br />
