 习近平保镖在新西兰国会大厦前对新西兰国会议员、新西兰绿党联合党魁罗素&#8231;诺曼（Russel Norman）博士动粗 (先驱报网站截图)<br />
 <br />
习近平保镖对新西兰国会议员动粗<br />
<br />
大叽歪记者徐亦扬、易凡新西兰综合报导）新西兰绿党联合党魁、国会议员罗素&#8231;诺曼（Russel Norman）博士由于反对中共对西藏的长期迫害而在习近平6月18日上午访问新西兰首都之际在国会大厦前向习近平展开雪山狮子旗。然而，他却被习近平的保镖粗暴对待，诺曼博士因此感到非常气愤，并准备向国会发言人进行投诉。 <br />
<br />
<img src="https://yopic.us/images/201006uwu.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
新西兰国会议员、新西兰绿党联合党魁罗素&#8231;诺曼（Russel Norman）<br />
博士在国会大厦前打开西藏雪山狮子旗 （绿党办公室图片）<br />
<br />
绿党国会议员诺曼博士向习近平展开了雪山狮子旗时被中国代表团成员看到这一幕，他们随即推搡诺曼博士，用一把伞袭击博士并撕扯他手中的旗子。诺曼博士撇开试图挡在他面前的伞，但中国保镖们夺走了诺曼博士手中的旗子，将它扔到地上并践踏，诺曼博士因此和他们发生冲突。诺曼博士向这些保镖喊道，你们可以在中国镇压言论自由，但这里是新西兰。<br />
<br />
诺曼博士事后向在场记者表示他对此事非常震惊。他说：“中共的保镖到我们国家来，在我们自己的国会门口推开一个民众选出的国会议员而仅仅因为我们支持民主与自由，我认为这简直是极其蛮横无理！”<br />
<br />
诺曼博士说，中国的保镖人数看上去比新西兰本国的保安人员要多，他说：“中国保镖用手将我推开，他们从我手里抢走了旗子，但新西兰的保安人员却并没有给予很大帮助。”保安放开他后，诺曼博士继续喊道：“你能在中国剥夺人权，但是在这里不行！你能在中国压制自由和民主，但是在这里不行！”他也告诉中国访问团的人们：“别把你们非民主的东西带到我们国家来。”<br />
<br />
诺曼博士说，他从来没在国会遇到过这种待遇，说的同时手上还有一些血迹。“我一定会将此事告知议会发言人，并跟他讨论外国的保镖在国会山庄的场地粗暴对待一个本国国会议员，这样的行为是否能被接受。我认为这是绝对不能被接受的事情。”<br />
<br />
现场目睹整件事情的一名媒体记者表示，中国保镖对诺曼博士非常粗暴并形容整个事件过程“非常离奇”。他说：“我从来没有见过一个国会议员被如此残暴对待过。诺曼博士被禁止靠近习近平附近的任何地方。”“如果一个人挥舞西藏的旗子就明显地被视为冒犯中国。但我们生活在一个民主的国家，我们应该能够像诺曼博士一样来表达我们的观点和意见。”<br />
<br />
绿党在随后发表的声明中称，诺曼博士在自己国家的国会被外国保镖虐待已经揭示出主张民主和人权是相当重要的。<br />
<br />
诺曼博士在声明中说：“因为约翰&#8231;凯伊（John Key）领导的新西兰政府让中国保镖控制了我们的国会，因此国会不再是一个能安全表达民主的地方。我已经向警方报案，因为新西兰人应该知道，新西兰民众有权利在没有暴力和指责的环境下自由发表言论。”<br />
<br />
诺曼博士也表示，政府必须保护本国公民自由发表言论的权利，因为它是民主的基石之一。他说： “我正在要求总理发表一份明确的声明，以表示这样的行为在新西兰是不能被接受的。”<br />
<br />
国会发言人洛克伍德&#8231;史密斯（Lockwood Smith）称，他会对此事进行调查。<br />
<br />
这起外交事件的出现迫使习近平和工党领袖菲尔&#8231;高夫（Phil Goff）的会议改变了地点。工党发言人说，来宾和庆典办公室告知改变会见地点，但没有告知理由。<br />
<br />
诺曼博士的政治和媒体顾问凯文&#8231;李斯特（Kevin List）先生告诉本报记者，诺曼博士目前正在警察局录口供，他在今天的事件发生后感到非常疲惫。</div>

							
							
							
							
