																											<div id="postmessage_691749" class="t_msgfont">习近平抵新西兰国会 出现突发事件 <br />
2010-06-18 <br />
<br />
<img src="http://imgly.net/img/597100.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
事发现场图片（图片来源：路透社）<br />
<br />
<img src="http://imgly.net/img/791a7f.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
事发现场图片(图片来源：路透社）<br />
<br />
<img src="https://yopic.us/images/2010061814.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
据新西兰《自治领邮报（Dominion Post）》6月18日（周五）报道，中国国家副主席习近平周五抵达新西兰国会时，一名中方随从撕毁了新西兰绿党国会议员Russel Norman握在手中的一面西藏旗帜。Norman表示，他认为该名男子是习近平的保镖。<br />
<br />
习近平一行六辆车的车队由警车开道抵达国会，这些中方保彪起初是用身体和雨伞挡住Norman手中的旗子，随后习近平的一名随从抢过并撕毁了该旗子并踩之脚下，这时习近平一行快速通过国会大门。　<br />
<br />
Norman对这些中方男子说：“别把你们不民主的行动带到我们国家。”<br />
<br />
事后Norman气愤地表示，他不能100％肯定那些人是中方保镖，“但看上去很象。”<br />
<br />
Norman说：“这些中方保镖能跑到我们国家来，还推撞民选的国会议员，原因仅仅是我站在自己的国土上，在自己的国会表达民主与自由，想到这真让人恼火。”<br />
<br />
“这些中方保镖，先是用肘子撞我，让我让开，又在我头顶上撑开了一把雨伞，然后抢走我手中的旗子并踩在脚下。”<br />
<br />
事发时，Norman手持一面西藏旗帜，并向习近平高呼“民主与人权”。<br />
<br />
（点击看原文）<br />
<br />
事件最新进展<br />
<br />
警方将对绿党领袖Russel Norman的投诉展开调查。惠灵顿警方长官Peter Cowan表示，警方是当天下午接到Norman博士的投诉的。<br />
<br />
现场众多照片清晰显示，Norman博士当天下午在挥舞一面西藏旗帜抗议时被陪同中国副主席的一名中方安全人员用手抓住。<br />
<br />
Cowan警官说：警方将对Norman博士的投诉展开迅速调查，结果如何目前尚言之过早。警方将与目击者谈话，并观看现场的照片与录像，及与依法发放在国会抗议许可的国会发言人核实情况。<br />
