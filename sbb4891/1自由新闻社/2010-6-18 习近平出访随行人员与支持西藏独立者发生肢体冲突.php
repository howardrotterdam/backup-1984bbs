													<h2>MP to lay assault complaint after protest</h2>
						
						
																											<div id="postmessage_691753" class="t_msgfont">MP to lay assault complaint after protest <br />
The Dominion Post Last updated 15:06 18/06/2010<br />
<br />
Green MP Russel Norman will lay an assault complaint with police after officials in Chinese Vice President Xi Jinping's entourage tore a Tibetan flag from the MP at Parliament today. <br />
<br />
Arriving in a convoy of around six vehicles with a police escort, the guards initially tried to shield the flag with their bodies and umbrellas before it was torn from Dr Norman's hand with a member of the vice president's entourage standing on it as the VIP was rushed through the front entrance of Parliament. <br />
<br />
''I think it's pretty outrageous that Chinese security can come to our country and push around an elected Member of Parliament simply because you're standing up for democracy and freedom in our own country on our own parliamentary grounds,'' Dr Norman said afterwards. <br />
<br />
''I mean, the Chinese security guards, they elbowed me out of the way, they put an umbrella over the top of me and they took the flag out of my hands and trampled on it.'' <br />
<br />
With some blood on his hand following the scuffle, Dr Norman said he had never experienced such treatment on Parliament's grounds. He believed it was one of the vice president's security guards. <br />
<br />
''We were roughed up, they grabbed us and pushed us around.''&nbsp; &nbsp;He said he was pushed when he tried to grab the flag back. <br />
<br />
Around 30 pro-Chinese government supporters were on site to welcome the second-most powerful man intheir country. <br />
<br />
''Don't bring your undemocratic processes to our country,'' Dr Norman had told the Chinese VIPs. <br />
<br />
Dr Norman said he would be speaking with the Speaker of the House about the incident. This afternoon he also indicated that he would lay a complaint with police. <br />
<br />
After meeting the vice president, Speaker Lockwood Smith said he would investigate the incident but had not seen it. <br />
<br />
Meanwhile, the diplomatic incident appears to have forced a change in the venue for a meeting with Labour leader Phil Goff. <br />
<br />
Mr Goff's office confirmed that a scheduled meeting at Parliament between him and the Chinese Vice President had been moved to the Hotel InterContinental. <br />
<br />
A spokesman said the change in venue had been advised by the Visits and Ceremonial Office but they had been given no reason for the switch. <br />
<br />
VIP TREATMENT <br />
<br />
Relations between China and New Zealand had so far on this trip been at an all-time high, with Prime Minister <br />
<br />
John Key ''pulling out all the stops'' for Mr Xi's visit. <br />
<br />
Ad Feedback Last night Mr Key was host at a dinner held for the Vice President at Government House in Auckland. <br />
<br />
Mr Xi's visit yesterday included his first public speech to a major business audience in New Zealand. <br />
<br />
Mr Key said Mr Xi's visit was part of an important exercise in diplomacy. <br />
''Our understanding is that there is a very good chance that Xi Jinping is going to be a potential future president of China,'' he said. <br />
<br />
''In the pecking order, he is right up there to replace President Hu.'' <br />
<br />
Mr Key rearranged his schedule to meet Mr Xi. He was forced to use an air force plane to make it to Auckland in time from a visit to Bay of Plenty. <br />
<br />
''We're definitely pulling out all the stops,'' he said. <br />
<br />
Mr Xi is meeting all New Zealand's top elected officials during his three-day visit. There will be further talks with Mr Key this morning, followed by lunch with Deputy Prime Minister Bill English at Premier House inWellington. <br />
<br />
Mr Xi will also meet Speaker Lockwood Smith and have talks with Labour leader Phil Goff today. <br />
<br />
Mr Xi, who is travelling with about 120 Chinese businesspeople, told an audience in Auckland that he wanted to elevate bilateral relations. <br />
<br />
A summary of his translated speech said there had been frequent exchanges of high-level visits and the economies of the two countries were highly complementary ''with huge potentials in economic and trade co-operation''. <br />
<br />
Mr Xi proposed a four-point plan, the first focusing on New Zealand's rich resources and its advanced science and technology. <br />
<br />
China had large markets and cheap labour. <br />
<br />
''So the two countries can put the improvement of economic and trade quality as their future focus.'' The two could also increase co-operation in new energy, environment, biological technology and high and new technologies. <br />
<br />
Trade Minister Tim Groser said China had played a major role in sheltering New Zealand from the full impact of a global recession. <br />
<br />
''I believe that more and more New Zealanders understand this and very much appreciate the contribution China is making to our own economy and society,'' he said. <br />
<br />
Mr Norman earlier said the top-level visit would add pressure from China to win approval for a bid to buy thousands of hectares of dairy farm land from the Crafar family. <br />
<br />
But Mr Key said the Overseas Investment Office, which must approve the bid, was independent. <br />
<br />
''There is no correlation between this trip and the desire by the Chinese consortium to buy the Crafar farms.'' <br />
<br />
This year China became New Zealand's second-largest trading partner, overtaking the United States and Japan after the signing of a free-trade deal between the two countries in 2008. <br />
<br />
In the year to April, the deal sparked an $860 million boost in Kiwi exports to China.</div>

							
							
							
							
