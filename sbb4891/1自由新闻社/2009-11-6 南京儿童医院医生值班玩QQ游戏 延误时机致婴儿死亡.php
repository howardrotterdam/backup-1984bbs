													<h2>人民网也关注了一下下</h2>
						
						
																											<div id="postmessage_319644" class="t_msgfont">婴儿家属反映：婴儿住院病情恶化时，几次向值班医生求救，却都遭到拒绝。<br />
<br />
　　院方解释：因孩子病情变化太快，从入院到患儿死亡不到24小时，超出我们对眼眶蜂窝组织炎这个疾病的认识之外。<br />
<br />
　　<br />
<br />
　　近日，南京一名5个月婴儿死亡事件引起社会广泛关注：婴儿徐宝宝因高烧、眼眶部肿胀等症状于11月3日中午入南京市儿童医院住院治疗，次日清晨5点多钟不治身亡。<br />
<br />
　　据婴儿家属反映，婴儿住院病情恶化时，家属几次向值班医生反映病情，由于医生打游戏、睡觉等原因，都未得到及时有效救治，由此导致了婴儿病情急剧恶化最终死亡。<br />
<br />
　　11月8日，记者对医患双方进行了采访调查。<br />
<br />
　　值班医生：“我不要睡觉了吗？”<br />
<br />
　　据婴儿父亲的书面陈述：11月3日上午，他带婴儿挂急诊在南京市儿童医院眼科门诊诊治，眼科医生诊断为蜂窝组织炎，安排婴儿住院接受治疗，并在病历上注明“住院后眼科医生结合内科医生马上进行会诊”，但是直到次日清晨5点多婴儿死亡，医院方面并没有进行“会诊”。下午6点多钟，婴儿眼部肿胀发展到脸部也肿胀，家属找到值班医生说明情况时，该医生称自己“是值班医生不是管床医生，婴儿情况不清楚，要等次日管床医生来了再说”。经亲属交涉，该值班医生到病房查看了一下病儿，没有采取措施。<br />
<br />
　　婴儿父亲陈述：11月 4日凌晨1点多钟，在病儿哭得更厉害、眼部肿胀加剧的情况下，亲属抱着病儿又找到值班医生请其查看病情时，遭到该医生拒绝。在亲属请求之下，该值班医生用棉签擦拭了病儿眼部，并发牢骚说“晚上把我叫起来，我不要睡觉了吗？”又过了10多分钟，家属再次抱病儿请值班医生查看，该医生仍表示要等次日管床医生来了再说。随后，婴儿母亲找到护士站向护士求助，“护士的回答是，值班医生晚上一般都是睡觉的，今天都被叫起来几遍了，很生气。”清晨5点多钟，婴儿变得呼吸微弱，婴儿亲属三人呼叫着去敲医生的门，一位医生听说病儿挂的是眼科，就又掉头关门睡觉了。最后在家属的请求之下，该医生打了几个电话请来医生抢救时，婴儿已经没有呼吸了。<br />
<br />
　　婴儿亲属的反映材料在南京当地知名网络论坛发布后，引起当地众多网民的关注，很多人发帖纷纷谴责值班医生，痛斥有关医护人员的失职和医德缺失。<br />
<br />
　　医院：孩子病情变化太快<br />
<br />
　　针对这起婴儿死亡事件和网络文章，南京市儿童医院医务处于11月7日发布情况说明，称网络上一些文章的内容与事实不符，并将医院对婴儿的救治过程及这起医疗纠纷的有关情况进行了说明。<br />
<br />
　　针对婴儿亲属提到的几个时间节点的救治情况，南京市儿童医院医务处做了对应解释：当日20：00左右，患儿父母因患儿哭闹找医生，值班医师来到床前查看患儿，当时患儿右眼较为肿胀，有感染症状，告知其抗生素、脱水剂已挂，嘱其勤点药水；11月4日1：30患儿家属将患儿抱至值班室，值班医师查看患儿，发现该患儿眼部分泌物较多，遂用棉签清洁患儿眼部，清洁时患儿哭闹不止，清洁完将棉签交给患儿母亲，嘱其勤点药水，以局部抗炎；凌晨2：20患儿父亲携患儿至值班室，接诊医生查看患儿右边面部肿胀，此时患儿已经入睡，考虑可能是体位压迫造成，嘱其多观察；5：50患儿突然面色发灰，立即给予吸氧，胸外按压，并请麻醉科，内科住院总，二值班会诊。给予气管插管，加压给氧，静脉推注肾上腺素，地塞米松，苏打，患儿一直没有恢复自主呼吸，7：30抢救无效，宣布死亡。<br />
<br />
　　南京市儿童医院医务处最后做出这样的解释：“因孩子病情变化太快，从入院到患儿死亡不到24小时，超出我们对眼眶蜂窝组织炎这个疾病的认识之外。”<br />
<br />
　　焦点：医护人员是否失职<br />
<br />
　　南京不少关注此事件的市民表示，医院医务处的解释虽然很客观，但是有两个关键问题没有讲清楚：一是医院对婴儿的救治是主动还是被动，二是死者家属强烈质疑的值班医生失职与否也未作说明。<br />
<br />
　　南京市儿童医院医务处表示，医院目前已经封存了婴儿住院的病历以及病区的监控录像，目前医院对此事件的处理有4条途径：1.医患之间协商解决；2.在鼓楼地区医患调解中心主持下的调解协商；3.进行医疗技术鉴定明确责任；4.诉至人民法院的诉讼途径。医务处同时表示，医院正在对当事相关人员进行进一步的调查了解。<br />
<br />
　　5个月婴儿死亡原因究竟是如其家属所陈由于医护人员失职、病儿没有得到及时救治所致，还是如南京市儿童医院解释的由于婴儿病情变化太快、超出医院方面对疾病认识的原因，目前还无准确定论。有关后续情况，本网将继续关注。<br />
