事情经过：（受害者是我的朋友，以下是我朋友给律师写的事情经过，请各位能耐心看下去）<br />
2009年11月1号宝宝发热，我们上午到南京江宁区医院检查，医院安排我们住院并进行治疗，第二天发现宝宝右眼睛处有红肿现象，我们通知医生，医院安排眼科医生会诊，初步诊断为脉泪肿，给我们开了红霉素眼膏和氧氟沙星药水。第二天早上宝宝眼睛进一步红肿，医生诊断后建议我们转儿童医院进行治疗，<br />
2009年11月3日上午我们带着发烧的宝宝到南京市儿童医院挂了急疹，眼科医生徐再兴进行诊断并进行血检，一般半个小时就可以拿到报告，血检处的人让我们等一个小时后拿说要复查，我们拿到报告后交给徐医生，他说报告显示血相不好，他诊断眼睛病症为蜂窝组织炎并安排我们住院接受治疗，要求住院后眼科医生结合内科医生马上进行会诊，并在病历上特别注明。<br />
11时左右我们把相关资料交给住院部眼科医生并办好住院手续入住14楼眼科45床，坐在前台的陈娟医生和冯小津医生就坐在那里询问了一下病情，没有给宝宝安排内科医生会诊，以致错过了最佳诊断治疗时期。我们住院后医生也没有到床前查看宝宝病情，入住后很长时间没有人来给宝宝量体温和挂水，我们反复到前台催，当班护士说药水还在配等到下午再挂，在我们再三催促下在一点半左右才给发烧的宝宝挂上水。从入院到宝宝离开，没有一个护士过来给宝宝量体温，每次都是我们自己到前台要量完温度后再告护士宝宝温度，在此期间也没有医生、护士到床前询问查看宝宝病情。<br />
接近中午时医生让我们下午去带宝宝做眼部CT，说等结果出来后会采取相应治疗措施。下午二点在医院拿单子给宝宝做了眼部CT，拿到结果后交至医生处，医生简单说了下没问题，并说她马上下班，但把我们的资料和情况交待给晚上的值班医生，并未像她之前所说CT结果出来后会采取相应措施。后来老婆询问宝宝的眼睛炎症问题，她说没事，这个得慢慢消炎，一天看不出效果的，要几天后再能看出来。老婆问以医生的临床经验来看宝宝的眼睛应该不会是大问题吧，医生说这个主要是炎症一般问题不大，老婆才安心下来。晚上六点多钟的时候我们发现宝宝的眼睛肿得更大了脸也肿了起来，<strong>我们就跑到值班医生毛晓君的办公室找他，当时他正在打游戏</strong>，我们说宝宝现在情况比中午严重了请您过去看看，他说他是值班医生不是我们的管床医生，小孩情况不清楚，得等明天管床医生过来再说，我老婆说你是不是眼科医生，他说是的，我老婆又跟她说白天的管床医生说过会给我们相应的资料给他，如果宝宝有任何情况可以直接找晚班的他。接着老婆说那我们求您过去看看好不好，这时他才很不情愿的跟着我们到了病房，他来到我们宝宝面前见扫了一眼就说没什么有什么情况等明天再说吧。<br />
