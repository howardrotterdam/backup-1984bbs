<br />
【新民网・独家报道】7月1日17时许，普陀区东新路武宁一村门口，四辆城管执法车辆被附近多名小商贩团团围住，动弹不得。期间，有小商贩爬上城管车辆，拉开白色横幅，上书“还我公道、要求严惩”等字样，并向过路行人展示一些照片。随着围观者越聚越多，东新路渐渐水泄不通，警方、120急救车也陆续到场。现场一度聚集了近百名围观群众。<br />
<br />
　　・缘起・<br />
<br />
　　2009年6月29日晚，一则网贴开始在天涯、宽带山等热门网站上流传。网贴中写道，“晚8：30分左右，在上海市普陀区武宁路西宫，城管开始了扫摊大行动！大概有80-100人左右身穿迷彩服的执法人员。刚开始的时候在路边先是把地摊搜走，后来竟然进入小区进行地毯式搜索，结果搜到好多躲在小区里摆摊的人……一个男孩开始追赶城管想要回自己的东西。在争执了大概10分钟左右大战的序幕就此拉开……”<br />
<br />
　　30日晚，新民网记者实地走访东新路。对此事件，人们的陈述并不完全相同。一个卖报纸的小伙子压低嗓门表示，“死人了”。一个临街店铺的店员回忆，“听说有人被打伤”。一个发廊小妹一脸迷茫地说，从未听过此事。但在与近20个受访者攀谈后，事件的基本脉络似乎已经清晰――“执法人员”人数众多，“小贩”和“迷彩服”之间因街道摆摊有过冲突，有人在事件中眼部受伤，“阵势”之大实属罕见。<br />
<br />
　　・历史・<br />
<br />
　　东新路不长，前后约两个街区。因年轻人的“潮人圣地”新西宫正坐落于此，短短的街道上热闹异常。<br />
<br />
　　东新路上历来摆摊者众多，卖烧烤的摊位，卖化妆品、小饰物、流行服饰、电子产品的摊位常年占据了街道两侧。当地居民虽颇有怨言，但因市口好、人流大，前来摆摊的小商贩一直络绎不绝。规范管理极有难度，长久以来是一个老大难问题。<br />
<br />
　　一位临街卖臭豆腐的中年女子告诉新民网记者，这条街以前也经常整治乱摆摊现象，但一般是路西口喊“城管来了”，小贩们就都从路东口逃走，或者进附近小区避一避风头就好。如29日晚那样的大规模整治行动，比较罕见，而有人受伤，更是闻所未闻。<br />
<br />
　　・当事人・<br />
<br />
　　新民网记者辗转联系上了在29日晚冲突中受伤的小廖，小廖说，他的左眼在冲突中受伤，里外都缝了针。据小廖称，当时他们不是在街上摆摊，而是在武宁一村内自家住宅门口摆摊卖鞋。远远听到街上有动静，之后有身穿迷彩服的执法人员冲进小区进行收缴。小廖的妹妹为护住货品，用身子挡在了“迷彩服”面前，但很快手就被扭在身后，肚子上也挨了两脚。小廖与妹妹的男友看不下去，便上前推搡“迷彩服”，随后双方扭打在一起。<br />
<br />
　　小廖妹妹称，当晚，“迷彩服”拿着疑似“凳子腿”的铁棍围成一圈殴打她的哥哥，目前，医生也无法断定小廖的眼睛会怎样。小廖的妹妹向记者表示，他们一家不需要什么赔偿金，现在只想揪出打伤她哥哥的人，“他们打伤了人不能不负责！”<br />
<br />
　　・声音・<br />
<br />
　　记者就此采访了多位城管部门工作人员或曾在城管队伍中工作过的人。一位不愿透露姓名的城管部门工作人员告诉记者，“首先，穿迷彩服的肯定不是城管。其次，目前全市只有十余支拥有执法资格的城管队伍。最后，如果有暴力执法的行为，城管部门定当予以严肃处理。”但面对记者“‘迷彩服’队伍为何人组成，由何人管理”的问题，该工作人员显得很为难，仅表示，“这是各个区县各自处理的事情，有些事情不太好说。建议哪里出事问哪里。”<br />
<br />
　　新民网记者另向在东新路被围堵的一位普陀区城管人员求证，该人士对“迷彩服”人员的身份三缄其口。他仅透露，“迷彩服”不属于城管，他不清楚他们为何在城管的车里。<br />
<br />
　　一位资深城管人员在接受新民网采访时表示，“迷彩服”属编外人员，但如果在执法时伤人，相关的招聘或管理部门是要负责的，伤人者也要负责，同时要看执法是否合理，伤人的程度及被伤害人是否有挑衅行为等情况而定。该资深城管人员表示，城管雇用的临时人员和辅警性质差不多，执法时应该配合城管工作，但他们没有处理和处罚的权力。<br />
<br />
　　・结语・<br />
<br />
　　7月1日下午18时许，东新路新西宫门口依旧被堵得水泄不通。自称在29日冲突中受伤的小商贩小廖一家，手持展示伤口的照片，一脸愤怒地站在一辆城管车辆上。<br />
<br />
　　而在路的另一边，卖烧烤、卖凉鞋、卖化妆品的小商贩依旧在不紧不慢地做着生意。有摊主起身，叉腰，眺望着不远处发生的这场冲突。</div>

							
							
							
							
