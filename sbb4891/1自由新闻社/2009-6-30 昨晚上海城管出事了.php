													<h2>昨晚上海城管出事了</h2>
						
						
																											<div id="postmessage_143235" class="t_msgfont"><a href="http://bbs.hualongxiang.com/thread-5354359-1-1.html" target="_blank">http://bbs.hualongxiang.com/thread-5354359-1-1.html</a><br />
6月29日晚8点半左右时间开始(具体时间没看).武宁路新西宫门口出现一批身穿绿色迷彩,带着头盔,手持警棍(铁棒)从一辆卡车上下来大约30~40人左右袭击小商贩,最后连路人都打.<br />
<br />
我亲眼看见这一群人十几二十个的围绕着几个手无伏击之力的小商贩用棍子猛锤小商贩头部/身体,小商贩倒下后十几个人还用脚踩.<br />
<br />
群众路人看不过去了,很多人开始叫唤,站在我身边的一个老头,手里抱着一只小狗,站在一旁大叫说: 没有天理,这样是土匪%&amp;$($^$#...&quot; 还没说完一群穿迷彩的人群就涌上来,又用棍子敲其头部,顿时那个老头就倒下了.我站在旁边被散开的人群一起退到了后面,随后又听到有人的惨叫声,黑夜里看到的就是空中挥舞的棍子,路人的惨叫声.<br />
<br />
过了大约5分钟,(送迷彩人来的车已经先开走,原本被打的倒在车轮胎下的人被他们里面的人拖走了)迷彩人大概觉得打出人命了,并且旁边的群众越来越气愤,随后他们就开始沿着武宁路往大马路走去,很多群众尾随其后,有很人气不过,拿鞋子扔,但是那些禽兽般的人回头就是一棍子,我亲眼目睹至少10人受到了棍击.有路人想用手机拍摄被那些人一把夺走手机后又用皮带抽打.他们一边撤退一边随手打骂他们的路人...<br />
<br />
我跟到了一个转弯口后又折回去看那个受伤的老头,那个老头就在我旁边,因为职责那群人被打的头出血,但在我再回现场时老头已经不见了,只剩下3个伤势严重的人.一个男的眼睛出血,(别人说眼珠也掉了)已经不会说话了,我猜测他是死了,另外一个男的满头是血,嘴巴睁开着,不知道在说什么.还有一个女的,半坐着,浑身是血.我急着拨打120救护车,对方说没有救护车了,你们自己救.这也是很让我难以接受的一件事.东方电视台的电话也是怎么打也打不通的.<br />
<br />
我冲在离伤者最近的地方,真的是太残忍了,3个好好的人,被打的体无完肤,我看的都哭了,警察说现在没有救护车,要不先自己送去医院,但是那个唯一能说话的女的说:<br />
<br />
我们不走,人都这样了,要等新闻媒体来,不播出来,谁为我们主持公道?!<br />
<br />
真的很难过,就在上个月,我在黄浦区看见一个十三点青年跳楼,来了武警,特警,消防队,救护车一大帮子人,那时我还感慨中国终于向发达国家一样拥有完整的法制了，但今天,人都这样了,120打去让我们自己救人,电视台电话永远在通话,警察估计最多来了3个后来慢慢群众多了警察也接二连三的才来了.无辜受伤的人还不如一个为挽回女友要跳楼的十三点.我真的不明白这个到底是区域的差别呢还是真的像其他小商贩在旁边说的他们是官官相互?<br />
<br />
太详细的现场我无法描述,但是实在是太残忍太混乱了,我觉得每个有血性的人都不想看到这样的场景再发生.不管那些迷彩的人是不是所谓的城管请来的打手,但真的不该这样没有人权没有人性的去殴打群众殴打一个人!<br />
<br />
PS:还有一幕,就是一个老太太看到伤者后心痛的大叫说没有法制没有人权这个社会乱套了,然后又说她不怕死,那么小的孩子被打成这样(伤者大约20岁左右),那些流氓要打就打她,她年纪大了无所谓了.说完这话旁边有几个15,16岁左右的小女孩子就开始笑,嘲笑这个老太,嘲笑她的话,嘲笑周围的人那么激动,嘲笑别人口中描述伤者的伤势.我很不能理解的瞪了她们一眼,她们居然还能继续笑.我很难过的和徐斌说如果受伤的是他们的父母他们会怎么样?继续笑吗?她们没有人性吗?(不是不可能的,因为那群人他们殴打的有很大一部份人不是小商贩是有正义感的路人.)我以前都不鄙视所谓的非主流,所谓的90后,但今天看到那2个笑的很开怀的小女孩我真的觉得很多人说90后脑惨是对的,因为他们生活的太幸福了,他们根本不懂什么叫同情什么叫有正义感,她们只觉得自己是最棒的,那个老太的话多可笑，多不切实际,她们根本不懂什么叫做人性.那群殴打群众的迷彩人没有人性,那两个现场笑的乐翻天的女孩子她们长大后会是什么样?估计也是对生命漠然的禽兽<br />
<br />
然后有人发了一个链接，是另一个人发的内容，而且有图片：<br />
<br />
6月29日晚，大概在8：30分左右在上海市普陀区武宁路西宫，城管开始了扫摊大行动！大概有80-100人左右身穿迷彩服的执法人员。 <br />
&nbsp; &nbsp;刚开始的时候在路边先是把地摊搜走，后来是在超市门口的冰柜冰箱只要是出位的全部拉走。 <br />
&nbsp; &nbsp;搜完了这些地摊和超市门口的摊位之后，后来竟然进入小区进行地毯式搜索，结果搜到好多躲在小区里摆摊的人，最后没收了一个大概50岁上下的中年妇女的东西《物品不详》，这个中年妇女带着一个女孩《关系不详、年龄大概16-20岁左右》，一个男孩《关系不详、年龄大概18-24岁左右》，开始追赶城管想要回自己的东西。 <br />
&nbsp;&nbsp;结果在争执了大概10分钟左右大战的序幕就此拉开，先是发生小的争执，后来在一片混乱之中那个小男孩的鼻子出血了，这下群众都乱了，开始在旁边大叫“城管打人了”。 <br />
&nbsp;&nbsp;又争执了几分钟，大战开始升级，那些城管想拉开她们三个人，结果动起手来，开始的时候是20-40个人围在那里对其3人拳脚相加，后来见大概20个城管人员有的拿着大概10公分宽30公分唱的皮带对其3人进行殴打，有的拿着城管竟然拿出60厘米长直径大概2CM的钢管对其3人进行殴打，场面简直是惊人，周围的群众沸腾了，对其暴力执法行为都表示严重不满，有的扔矿泉水瓶、有的扔拖鞋进去，但是那些城管依然不管继续殴打其3人，大概打了2翻之后，3人躺在马路上不能动弹，浑身是血，造成交通堵塞，后来110来了，打人的城管走了，凄惨的3人满身是血的瘫在地上。 <br />
