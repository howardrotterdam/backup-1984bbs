一孩政策30周年　美议员轰最侵人权<br />
<br />
昨日是内地实施只生一个孩子的「一孩政策」30周年，官方媒体纷纷刊文纪念，肯定该政策对社会发展的正面作用；但也有专家痛批该政策违反自然规律，令新一代伦理残缺，中国提前进入老龄社会。美国则有国会议员继续炮轰中国一孩政策侵犯人权。<br />
<br />
1980年9月25日，中共中央发表公开信，提出为控制人口过快增长，要推行「一对夫妇只生育一个孩子」政策。由此，备受争议的一孩政策正式在内地全面推行。连日来，中央各大媒体纷纷发文，肯定公开信的历史作用，指一孩政策「有效控制了人口过快增长，促进了经济发展、社会进步和民生改善」。<br />
<br />
吁美施压促华取消政策<br />
<br />
但官媒同时坦承，中国人口发展正面临「前所未有复杂局面」，「人口素质有待提高，性别比例出现问题，人口老化加速」。中国社科院教授、全国政协委员叶廷芳指，一孩政策极不明智，违背自然规律，破坏了人的精神生态自然，造成伦理残缺的生存环境，独生一代变得冷漠、自私、脆弱。<br />
<br />
叶延芳还以台湾、香港、新加坡等地为例，指这些地方人口密度都很大，但经济不断发展，民众生活水平不断提高；他直指，一孩政策是非科学思维产物，威胁中国人子孙後代精神健全和健康，应尽快废除。是否全面取消，在内地一直有争议。<br />
<br />
前日，坚决反对堕胎的美国共和党议员克里斯。史密斯（ChrisSmith）炮轰中共一孩政策，指那是30年来「最违反人权的政策」。他指，曾接触各种人权个案，没有任何政策像中国的一孩政策那样，对如此多妇女和儿童造成严重伤害；他呼吁美国继续施压，促中共取消该政策。<br />
<br />
中共表面企硬，不放弃计划生育政策，但内部正进行微调。媒体公开报道显示，近年来先後有北京、上海、山西、广东、江西、陕西、湖北、重庆等省市，对人口生育政策进行修订，允许夫妇双方只要一方是独生子女，可生两个孩子。<br />
