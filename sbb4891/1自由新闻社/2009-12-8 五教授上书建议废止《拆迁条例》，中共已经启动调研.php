唐福珍的自焚并没有阻止房子被拆。<br />
<br />
<img src="http://img1.gtimg.com/news/pics/25160/25160945.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
根据录像，唐福珍几次往自己身上浇汽油，但她的自焚并没有阻止房子被拆。<br />
<br />
<br />
拆迁背后有着巨大的利益诱惑，现在根本问题是，我们没有制度去阻挡地方政府的利益诉求<br />
<br />
47岁的唐福珍在医院与伤痛搏斗了16天后，在11月29日孤独地走完了生命的最后路程。弥留之际，她的数名亲人或受伤入院或被刑拘，重症监护室被4名治保人员警戒，亲人无法探视。<br />
<br />
16天前,为了抗拒拆迁，唐福珍将汽油泼到自己身上，点火自焚。<br />
<br />
<br />
中国式拆迁更为惨烈<br />
<br />
唐福珍本是金牛区天回镇金华社区的女企业主，她的三层小楼建于1996年。2007年8月，成都市要在此地规划建设一项污水处理厂的配套工程，唐福珍家正好位于规划红线内。这幢建筑面积1000多平米的房子政府按1996年建房标准赔付117万元，而唐福珍一家希望赔偿800万元。<br />
<br />
两年的协调，双方始终未达成一致。今年4月10日，强拆开始了，金牛区城管执法局带着拆迁队浩浩荡荡，但在唐福珍的楼前，他们遭到了激烈抵抗。唐福珍及亲属向执法人员不断投掷石头、汽油瓶，这场拆除行动被迫取消。<br />
<br />
时隔半年多的精心准备后，拆迁队伍卷土重来，这次他们集结了更庞大的队伍，穿着迷彩服的人、消防队的战士，甚至医务人员，悉数到场。11月13日天未亮，力量悬殊的拆迁大战打响，亲属们继续用石块、燃烧瓶阻止拆迁，唐福珍则在天台，她提前备好了汽油。<br />
<br />
谁也不知道看着拆迁队伍，唐福珍在想什么，根据录像，只能看到当时唐福珍的肢体动作，她几次往自己身上浇了汽油，“你们退下，我们可以坐下来商量，否则我就要自焚了!”但无人理会，强拆继续进行,执法人员持械冲入，唐家人仰马翻。10分钟后，唐福珍点燃了身上的汽油，她变成一团火，很亮很亮，最终倒在自家的天台上，身后是一面五星红旗。<br />
<br />
唐福珍在被送到医院时留给家人的最后一句话是，“你一定要从我家里面选一张漂亮的照片做遗像”。但她的自焚并没有阻止房子被拆，当天房子就变成了一片废墟。<br />
<br />
唐福珍的悲剧上演前后，中国的拆迁之战正在多处爆发：在上海，被拆迁女住户潘蓉手持自制燃烧瓶阻挡掘进的挖土机；在贵阳，无奈的被拆迁居民用40多个液化气罐堵路讨要说法；在昆明，因为拆迁一个大型集贸市场，上千商户上街抗议……<br />
<br />
天南地北，形态各异，中国式拆迁所引发的对抗正逐步升级，方式则更为残酷惨烈。<br />
<br />
<br />
对抗历经三阶段<br />
<br />
2007年，重庆“最牛钉子户”吴苹打着维护物权法的旗帜，在媒体和网民暴风雨式的民意轰炸下促成了拆迁史上“一个伟大的标志性事件”。这让遭受同样境遇的被拆迁户多了幻想，但吴苹的幸运并没有在中国拆迁案例中复制。<br />
<br />
在中国政法大学宪政研究所所长、曾任全国人大常委会秘书组副组长的蔡定剑看来，当初是因为《物权法》出台时出现舆论高潮，地方政府对强拆稍有收敛。但实际上，从本世纪初至今，暴力拆迁从未停止，“有时地方政府只是潜伏一阵，等风头过后，还是会强拆”。<br />
<br />
近几年，地方政府的拆迁手段越来越强悍，抵抗的百姓还会被刑拘。前段时间，某直辖市的一份内部讲话资料曝光，其中明确提到强拆是一种合适的手段，并且要“形成巨大的压力，造成兵临城下的态势”。这次金牛区地方政府定性唐福珍事件为“暴力抗法”，并刑拘家属。正是种种强压，业主采取了玉石俱焚的抗议。<br />
<br />
国内知名评论人五岳散人在接受本报采访时认为，从最近的事例来看，拆迁中双方的耐心似乎都被消磨了，对抗的手段与结果都越发的激烈。<br />
<br />
“对抗从哀求式的上访，开始转变成伤害自身以求传播效力的最大化。”作为强势的一方，由于各种条件都具备，越发没有了协商解决的耐心与诚意，而住户方在财产损害更大、权益被侵害更多的状态下，也以更激烈的方式进行对抗。<br />
<br />
五岳散人把中国拆迁的对抗分为三个阶段：最初，对抗是单向压制，因为那时传播媒介是有限的；第二阶段是上访以寻求司法救济；第三阶段，伤害自身同时也伤害对方，有时集体对抗，更极端的是自焚造成社会伤害。<br />
<br />
<br />
《物权法》无力抗拆迁<br />
<br />
近期频发的暴力拆迁引起了中国社会的极大关注，也引起了世界的广泛关注，日本《产经新闻》就此评论认为，中国市民维权意识增强，“一方面是地方不顾形象的蛮横做法，另一方面是市民保护私有财产的维权意识逐渐提高，这种官民冲突的事件很可能越来越多。”<br />
<br />
于是，在拆迁的对抗中，有媒体人士将这样的场景形容为：一边是高举着支持依法强拆的《城市房屋拆迁管理条例》，一边摇动着本该更有力的《物权法》；一边是现实利益的铲车，一边则是铲车下迅即变成砖瓦石块的立身之所。<br />
<br />
“ 无法阻止拆迁，人们才发现《物权法》是没有牙齿的。”蔡定剑认为，2001年6月修改制定的《城市房屋管理拆迁条例》为拆迁矛盾频发埋下隐患。尽管 2004年修改宪法时，加强了对公民合法的私有财产不受侵犯的保护性规定，并在2007年出台了《物权法》，但拆迁条例并没有改变。<br />
<br />
不少地方政府的拆迁行为都是以拆迁条例来支撑，高高地举起推土机铲。<br />
<br />
<br />
背后的地方利益诉求<br />
<br />
与以往中国式拆迁不同，本轮强拆行为中，有不少涉及地方政府的公共建设，成都被拆迁户唐福珍和上海被拆迁户潘蓉的房子在拆迁后，前者用作建污水厂，后者则用作上海世博会的重点工程虹桥交通枢纽。<br />
<br />
去年，为抵御金融危机冲击，国家出台了规模庞大的4万亿投资计划，其中流向地方的基础建设占了很大比重，而当前宽松的货币政策也进一步刺激了地方政府投资热。“这次强拆集中爆发是由一个整体经济框架走向决定，地方政府需要消化这些资金，进而拉动GDP。”五岳散人告诉本报。<br />
<br />
在涉及公共项目和基建项目建设时，地方政府打着公共利益的牌子进行拆迁，如果有合理的赔偿，想必唐福珍和潘蓉不会以死抗争，但是，拆迁行为往往被利益集团和开发商利用，成为一种掠夺。<br />
<br />
“ 地方政府出现罕见的拆迁冲动，是因为巨大的利益诱惑。”学者袁剑这样分析。他认为，地方政府将土地从居民那里拿来之后，要么亲自拍卖，要么通过地方政府拥有的地产公司转手土地，一方面拼命压低买地价格，另一方面又拼命抬高卖地价格。这种具有经纪性商人的身份让地方政府成为交易中的一方。<br />
<br />
还有一位不愿透露姓名的专家则表示，这轮拆迁暴力也许仅是开始，“四万亿的效应还没有完全显露出来，一些央企地王还没开拆，将来对抗方式或许会更加极端激烈。”<br />
<br />
蔡定剑则认为，无论刺不刺激经济，原因在于地方政府从土地财政所获得的巨大利益，“拆迁背后有巨大的利益诱惑，现在根本问题是，我们没有制度去阻挡地方政府的利益诉求。”<br />
<br />
<br />
燃烧的现代化<br />
<br />
自焚的唐福珍在医院挣扎了16天，还是死了。<br />
<br />
那栋她试图用生命保卫的“违法建筑”如今仅余残迹，用不了多久就会消失得无影无踪。成都城北大天污水处理厂将会“正常运行”，金新路也会“全线贯通”，唐福珍的房子给“当地群众的生产生活和交通出行带来不便”的局面将一去不返。<br />
<br />
就算这些金牛区政府的上述自辩理由既冠冕堂皇又言之有据，人们还是会不禁问一句“值得吗”？用生命换来的“正常”还能算是正常？诚然先哲有云“不语怪力乱神 ”，但金牛区政府难道真的一点都不担心某个坊间传说会从此徘徊在污水厂上空？比如说走夜路时会看见一团刺眼的火焰在街边升起？<br />
<br />
当然，21世纪了，现在没有多少人还相信我们敬天法祖的传统，更别指望用这种涉嫌迷信的东西吓唬住那些拆迁公司和他们的幕后主使了，因为他们有一个可以横扫一切的理由――现代化。<br />
<br />
小区开发、道路升级、工业园建设，这些年来，打着现代化旗号的大规模建设在中国的每一个城市和乡村方兴未艾。漫天尘土中高楼大厦拔地而起，尽管很多困守蜗居的老百姓只能望楼兴叹；厂房林立，拯救世界经济的奇迹就在机器轰鸣中照进现实，尽管我们也因此使自己成为了排放巨头。<br />
<br />
金融危机期间，我们被很多国家看做是拯救经济的救星，甚至在电影中，“中国制造”已经成了人类的最后希望。然而，现实中的“诺亚方舟”并非建立在喜马拉雅山麓，而是诞生在响彻九州的机器轰鸣声中。<br />
<br />
没有人反对现代化，除了那些一厢情愿幻想在古老东方保持千年田园牧歌传统的欧洲养老金领取者（或者说是领取养老金的欧洲），但是问题在于我们究竟要一个什么样的现代化？在我们的现代化之路上，要拆掉多少栋房子？破坏多少亩田地，要有多少个唐福珍被碾在“前进”车轮下？<br />
<br />
上海、南京、青岛、赤峰，从金融中心到三线城市，不止一个成都的现代化进程因此而蒙羞，用“自我劫持”对抗暴力拆迁不起源于“暴力抗法”的唐福珍，也很难说会结束在“处置失当”的钟长林。唐福珍不是“前进”的落伍者，而曾是当地招商引资的楷模。这可真应了那句俏皮话“长江后浪推前浪，前浪死在沙滩上”，“前浪”尚且如此，那些在死水微澜中沉浮的平头百姓又当如何――深更半夜被“拆迁公司”破门而入，架起来扔到大街上？难道我们要的就是这样的现代化？<br />
<br />
更为重要的是，用这样的方式我们能实现现代化吗？试图用一句“处置失当”开脱责任的金牛区政府懂得什么叫做现代化吗？他们知道用错误方式是不可能解出正确答案的吗？他们是觉得用170万补偿款拆掉价值800万的房子是一笔划算的买卖，还是觉得靠暴力就可以保证人民的“正常生活”？再说一句诛心的话，谁能保证他们的目的真的是为了当地人民的福祉？<br />
<br />
我们要现代化，我们希望拥有现代化的道路，现代化的污水处理厂，但我们也要一个现代的法治社会，要拥有尊重人民生命权利的城管执法局及其背后的公共权力，我们要一个知道自己的前进需要代价并且敬畏这种代价的现代化进程。<br />
<br />
再过20年，不会有多少人还记得唐福珍，大天污水厂可以夜以继日地为一个清洁的成都服务，然而，万一锦官城的孩子们问起这座现代化污水厂的来历，我们这些大人可该怎么回答呢？（叶海林）<br />
