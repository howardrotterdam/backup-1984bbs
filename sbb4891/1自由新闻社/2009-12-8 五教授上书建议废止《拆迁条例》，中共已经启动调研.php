--------------------------------------------------------------------------------<br />
<br />
成都市自焚而死的唐福珍女士，是当今盛世的一大讽刺，也是对暴力拆迁最大控诉。要杜绝这类悲剧，当局必须痛定思痛，立即废除遗害人间十多年的制度。以前广州孙志刚案后，中央撤销歧视民工的城市收容遣送制度，因而有人寄望唐福珍事件也有相同结果。<br />
<br />
笔者对此，极度悲观。○三年三月，湖北大学毕业生孙志刚，在广州一间私人公司工作，外出时没带暂住证，遭公安强行送至收容遣送中心，后被虐打至死。《南方都市报》披露事件后，举国哗然，刚上台的温家宝，正锐意推行「胡温新政」，对此惨事还有点反应，遂下令取消城市收容遣送制度。<br />
<br />
一个民工死亡，引发如此巨变，令人始料不及。但之后像孙志刚一样枉死的人，多不胜数，却再没同样的奇迹。<br />
<br />
云南的李乔明在看守所离奇死亡，公安指他跟其它囚犯玩躲猫猫游戏，撞墙而死。这种侮辱公众智慧的谎言，惹怒亿万网民，当局改口牢头恶霸所为，而疏忽的公安只获缓刑。躲猫猫事件后，每月仍有人在看守所被打死。还有湖北石首市厨师的离奇命案，触发数万市民上街抗议；四川大地震后，近万学生葬身豆腐渣学校；三十万婴儿饮了国产的三聚氰胺毒奶而患上肾石……。这些w案，都无法推进社会变革。<br />
<br />
○三年八月，南京男子翁彪不满拆迁赔偿，愤而到拆迁办公室自焚而死；一个月后，安徽农民朱正亮也因住所被拆，无处伸w而于天安门自焚；六年后的今天，唐福珍女士复制了他们的悲剧。这六年间，因拆迁而自焚、自残，又或反抗时被打死打伤的平民百姓，成千上万，但对暴力拆迁制度，又有甚么冲击呢？<br />
<br />
<strong>古谚以「民不畏死，奈何以死惧之」去形容苛政，但今日盛世已是「官不畏民死」新时代，死再多人，官僚也无动于衷，自杀自焚又怎能改变甚么呢？当日的孙志刚案，碰巧胡温上台不久，要笼络人心；今天，有谁会为平民百姓而开罪地方政府。不要忘记，中央对地方当局也要投鼠忌器，要地方支持才能落实政策，而地方逾半财政收入来自土地，废除暴力拆迁，意味着收入大减，他们怎会甘心放弃？更何况，社会目前无法形成有效压力，迫中共非放弃暴力拆迁不可。</strong><br />
<br />
可以预见，唐福珍悲剧后，不仅不会改变暴力拆迁，日后楼价急涨、地价更高时，地方政府拆迁冲动更大，官民的拆迁冲突必会越演越烈。<br />
<br />
张华<br />
