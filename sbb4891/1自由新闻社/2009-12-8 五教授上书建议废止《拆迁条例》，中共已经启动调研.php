													<h2>国务院邀请5名北大建言学者研讨拆迁条例</h2>
						
						
																											<div id="postmessage_374261" class="t_msgfont">2009年12月15日 06:45人民网<br />
<br />
建言全国人大审查拆迁条例的五名北大法学学者接到邀请，参加明天由国务院法制办举办的座谈会，研讨《城市房屋拆迁管理条例》(以下简称《拆迁条例》)。<br />
<br />
5建言学者获邀参加座谈会<br />
<br />
本月7日，北大五名法学学者向全国人大常委会建议立法机关对《拆迁条例》进行审查，撤销这一条例，或由全国人大专门委员会向国务院提出书面审查意见，建议国务院对《拆迁条例》进行修改。昨天中午，5学者之一王锡锌接到了来自国务院法制办的电话邀请，要他们明天去参加座谈会，研讨《拆迁条例》。<br />
<br />
北大法学院姜明安教授表示，国务院法制办副主任郜风涛邀请他们参加这次会议，并将主持这次座谈会。北大递交审查《拆迁条例》建议书的五名法学学者分别是沈岿、姜明安、王锡锌、钱明星和陈端洪。不过陈端洪因事先安排了其他事务而当天无法到会。另据了解，除了这五名学者，还会有其他学者参加这一座谈会。<br />
<br />
对国务院法制办的这种做法，王锡锌表示，“这是比较积极的回应”。王锡锌说，之前通过私下渠道了解到国务院法制办对《拆迁条例》比较重视，“也有非正式的交流”。他也了解到这一条例的修改和废除会面临很大的阻力，但现在全社会都比较关注这一条例和拆迁问题，这是一个契机。<br />
<br />
人民日报呼吁修改拆迁条例<br />
<br />
此前媒体报道称，国务院正准备修改这个条例，目前已组织国务院法制办、住房和城乡建设部、国土资源部、农业部、林业局等相关部委，进行前期的立法调研工作。<br />
<br />
《立法法》规定，公民认为行政法规和单行条例同宪法或法律相抵触的，可向全国人大常委会书面提出进行审查的建议，由常务委员会工作机构进行研究，必要时，送有关的专门委员会进行审查、提出意见。王锡锌说，《立法法》只规定了公民有提起违宪审查的建议权，并没有规定立法机关如何回应公民的建议。沈岿也表示，有关部门不一定会开一个先例，对公民的建议要予以回复。<br />
<br />
沈岿表示，成都唐福珍为对抗拆迁而自焚事件促使他们提出这一建议。《人民日报》12月10日发表“人民时评”称，6年前的孙志刚案废除了《城市流浪乞讨人员收容遣送办法》，希望“悲剧性的个案最终能推动制度的进步”，让城市拆迁立法融入更多现代法治的文明基因，以避免同类事件的再度发生。<br />
<br />
评论称，在《物权法》出台之后，我国拆迁制度就受到质疑，此次如果能顺应民意和法治潮流，对《拆迁条例》中与上位法相冲突的地方进行彻底修改，无疑是众望所归。</div>

							
							
							
							
