													<h2>……唐福珍点燃了身上的汽油，她变成一团火……身后是一面五星红旗。</h2>
						
						
																											<div id="postmessage_361440" class="t_msgfont">国务院欲修改《拆迁条例》 已展开前期调研<br />
<br />
7日，全国人大常委会法工委法规备案审查室官员在接受本报记者采访时透露，国务院正在准备修改《城市房屋拆迁管理条例》(以下简称《拆迁条例》)，目前已经组织了国务院法制办、住房和城乡建设部、国土资源部、农业部、林业局等相关部委局，再次进行前期的立法调研工作。<br />
<br />
北大法学院教授沈岿亦向记者证实，其已获悉国务院相关部委正准备修改《拆迁条例》的消息。<br />
<br />
今年7月，《中国新闻周刊》曾报道，作为《物权法》的配套法规，原计划在2008年完成修改的《拆迁条例》，修改近两年却尚无音讯。<br />
<br />
昨日，沈岿联合北大四位教授，以公民名义致信全国人大常委会，建议废止或修改《拆迁条例》相关条款。<br />
<br />
昨晚，记者致电全国人大常委会法工委法规备案审查室，该室一位官员在了解了相关情况后表示，目前还没有收到这一建议书；至于能否给北大五教授反馈，“目前还没有建立对公民、法人、其他社会组织提出立法审查建议的反馈机制，正在研究怎么去反馈。”<br />
<br />
这位官员坦言：“不是不想去反馈。如果这项工作做不好，我们也就成了信访单位了。难度在于法规备案审查多涉及国务院行政法规、地方立法和规章等很多已经成型、生效的规定，怎么去操作，是个问题。”<br />
<br />
按照《立法法》规定，对于法规的审查权由全国人大常委会下设各专门委员会行使，由国务院、最高人民法院、最高人民检察院、地方人大、中央军委提出审查要求，由社会上的公民、法人和其他社会组织提出审查建议。<br />
<br />
据悉，2003年孙志刚案发生后，北京大学法学院三位博士许志永、滕彪、俞江就曾以公民身份建言全国人大常委会对当时的《城市流浪乞讨人员收容遣送办法》进行审查；2004年5月，全国人大常委会法工委法规备案审查室成立，并负责对审查要求和审查建议进行先期研究，确认是否进入启动程序，然后交由各专门委员会进行审查，是审查法规备案、下位法与上位法(尤其是宪法)的冲突和抵触的专门机构。<br />
<br />
