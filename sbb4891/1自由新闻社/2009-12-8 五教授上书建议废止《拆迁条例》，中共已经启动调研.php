													<h2>五教授上书建议废止《拆迁条例》，中共已经启动调研</h2>
						
						
																											<div id="postmessage_361202" class="t_msgfont">昨日，北京大学法学院五位教授以公民名义致信全国人大常委会，建议废止或修改《城市房屋拆迁管理条例》(以下简称《拆迁条例》)相关条款，此举获得从事拆迁法律服务的知名律师王才亮的支持。<br />
<br />
共同起草并签署这份建议书的北大法学院五教授中，宪法和行政法学者四人：沈岿、王锡锌、陈端洪、姜明安；民商法学者一人：钱明星。在这份建议书的最后，五位教授写上了各自的公民身份证号码。<br />
<br />
据悉，《拆迁条例》此次被质疑的内容共有18条，分别是第4、7、13、15、16条，第3章(共13条)。<br />
<br />
据沈岿教授介绍，这份建议书已于昨日通过特快专递和传真两种方式发往全国人大常委会。<br />
<br />
《拆迁条例》被指违法且过时<br />
<br />
“《拆迁条例》与《宪法》、《物权法》、《房地产管理法》保护公民房屋及其他不动产的原则和具体规定存在抵触，这导致了城市发展与私有财产权保护两者间关系的扭曲。”五位教授建议，立法机关应以法制协调统一原则为基础，对《拆迁条例》进行审查，以建立合法、公平、公正的房屋拆迁法律关系。<br />
<br />
五位教授一致认为：“(修改后的)《拆迁条例》自2001年颁布施行起，历经8年，其原有框架已经不再适应宪法、法律以及经济、社会的巨大发展。”<br />
<br />
法律规定补偿完才能拆<br />
<br />
五教授认为，依据《宪法》、《物权法》和《城市房地产管理法》规定，要通过征收获得公民房屋的所有权，必须具备三个法定条件：“为了公共利益”、“依据法律规定的条件和程序”、“给予补偿”； “补偿是征收的构成要件之一，未依法补偿，对房屋所有权及相应土地使用权的征收程序就没有完成；而征收没有完成，就不能进行拆迁。”<br />
<br />
律师建议完整修改涉拆迁法律制度<br />
<br />
“地方政府、开发商联手与民争利的现象在拆迁过程中普遍存在。”长期从事拆迁法律业务的北京才良律师事务所主任王才亮昨晚在接受本报记者书面采访时指出，在2001年6月修改《拆迁条例》后进行的拆迁中，获利的多是地方政府与开发商，而被拆迁人绝大多数是拆迁后买不起或得不到与被拆迁房屋相似水平的房屋，从而引发众多社会问题。<br />
<br />
对学者们的建议，王才亮表示认同，并更深一层认为，当前应该对整个涉及拆迁的法律制度进行修改。<br />
<br />
“仅修改《拆迁条例》并不能改变目前拆迁扰民的状况。”王才亮认为，应制定符合《宪法》规定的不动产征收法律法规，取代《拆迁条例》；按照《宪法》、《物权法》规定，修改《土地管理法》、《城市房地产管理法》、《城乡规划法》，彻底消除地方政府通过拆房卖地牟利的渠道；并且在上述立法中提高可操作性，重点规范对违法拆迁和听证环节规定罚则，遏制暴力违法拆迁。<br />
<br />
《拆迁条例》三问题<br />
<br />
●将本应在征收阶段完成的补偿问题延至拆迁阶段解决；<br />
<br />
●将补偿主体定位为拆迁人，将拆迁补偿关系界定成民事法律关系；<br />
<br />
●授权房屋拆迁管理部门在没有依法征收的前提下就可给予拆迁人拆迁许可。<br />
<br />
学者上书摘录<br />
<br />
实际执行中顺序如何被打乱<br />
<br />
五教授认为，依据《宪法》、《物权法》、《城市房地产管理法》，征收、补偿的主体都是国家，征收与补偿应该在同一阶段由国家来完成；国家依据公共利益需要，在依法补偿的前提下，对单位和个人的房屋及其他不动产实施征收。<br />
<br />
“因此，征收、补偿法律关系完全是行政法律关系，必须遵守依法行政的要求。”五教授指出，《条例》规定的补偿主体为“拆迁人”，是“取得房屋拆迁许可证的单位”，而不是代表国家实施征收的主体；基于这样的定位，把拆迁人与被拆迁人之间订立的拆迁补偿安置协议界定成民事法律关系。<br />
<br />
五教授强调，“正是由于这些规定对征收补偿法律关系的界定错误，致使一些地方政府在实际运作中只征收、不补偿，而把补偿这一核心问题和矛盾推到拆迁阶段，从而引发了大量的暴力拆迁、强制拆迁。”<br />
<br />
五教授建议，要解决当前拆迁引发的严重社会矛盾，必须废止或修改《条例》关于拆迁主体、拆迁补偿安置协议等与《宪法》、《物权法》、《城市房地产管理法》相抵触的上述条款。<br />
<br />
【法律法规互相抵触的地方】<br />
<br />
●谁来补偿？<br />
<br />
《宪法》、《物权法》、《城市房地产管理法》：代表国家实施征收的主体<br />
<br />
《拆迁条例》：取得房屋拆迁许可证的单位<br />
<br />
●拆迁人与被拆迁人的关系<br />
<br />
《宪法》、《物权法》、《城市房地产管理法》：行政法律关系<br />
<br />
《拆迁条例》：民事法律关系<br />
<br />
【被打乱的顺序】<br />
<br />
国家法律规定：先补偿，完成补偿才能完成征收，征收完成才能开始拆迁<br />
<br />
地方实际操作：引发矛盾的案例多是先征收(不含补偿)，然后边拆迁边补偿<br />
<br />
来龙去脉<br />
<br />
互相打架的法律法规<br />
<br />
1991年3月22日，为了与《城市规划法》相配套，国务院公布了《拆迁条例》。<br />
<br />
当时城市建设的主体多是国有单位，以政府为主导的拆迁行为，较少涉及公共利益和非公共利益的区别；但在1994年，随着《城市房地产管理法》出台，开发商逐渐走上前台；同年推行的分税制改革，被视为地方政府逐渐倚重土地财政的因素之一。<br />
<br />
10年之后的2001年6月，修改后的《拆迁条例》公布并沿用至今，但仍未区分公益和商业拆迁，其运作模式依然沿袭了建设单位向政府申请拆迁许可、获批后实施拆迁、发生纠纷由政府裁决、被拆迁人拒绝拆迁的实行强制拆迁等做法。<br />
<br />
2004年3月，现行《宪法》第四次修正案增加了“国家为了公共利益的需要，可以依照法律规定对土地实行征收或者征用并给予补偿”、“公民的合法的私有财产不受侵犯”、“国家依照法律规定保护公民的私有财产权和继承权”、“国家为了公共利益的需要，可以依照法律规定对公民的私有财产实行征收或者征用并给予补偿”等内容。<br />
<br />
2007年3月，《物权法》公布施行。该法第42条规定：“为了公共利益的需要，依照法律规定的权限和程序，可以征收集体所有的土地和单位、个人的房屋及其他不动产。征收单位、个人的房屋及其他不动产应当依法给予拆迁补偿、维护被征收人的合法权益，征收个人住宅的还应当保障被征收人的居住条件。”<br />
<br />
《立法法》规定，在我国法律体系中，宪法具有最高法律效力，其次是法律，再次是行政法规；按照一般法理原则，新法效力高于旧法。《拆迁条例》属于国务院制定的行政法规，其法律效力低于宪法和法律；五位教授认为“其相关内容已经与新法发生抵触或冲突”。<br />
<br />
