													<h2>马英九：纪念自由主义巨擘殷海光与雷震</h2>
						
						
																											<div id="postmessage_205655" class="t_msgfont">　　马英九总统今天下午出席殷海光先生逝世40周年与「自由中国」发行人雷震先生逝世30周年─「追求自由的公共空间：以《自由中国》为中心」学术研讨会，并应邀致词。<br />
<br />
　　总统致词内容全文为：<br />
<br />
　　今天适逢殷海光先生逝世40周年与「自由中国」发行人雷震先生逝世30周年，基金会举办「追求自由的公共空间：以《自由中国》为中心」学术研讨会，怀念这两位自由主义巨擘，个人有幸应邀来参加，感到非常高兴，更有点意外。<br />
<br />
　　事实上今年三月雷震先生逝世30周年，我曾经到他的墓上致意，今天又来这里，其实是有非常复杂的感受。因为殷老师在台大教书的最后一年，我修刘老师的逻辑课程时，他推荐殷先生的思想与方法，对他有了粗浅的了解。但对于在戒严的年代，38年代创立自由中国，需要很大的勇气。他在戒严时期还能够提出自由反GONG运动，回归民主宪政，追求自由的政治体制，并且后来发展成自由的反对党，这些过程对台湾的民主发展都具有关键性的影响，也启发了好几代的台湾青年在自由民主这条路上摸索前进。所以今天的研讨会具有特别的意义。<br />
<br />
　　我们都知道在「自由中国」时期，殷老师与雷先生两人的风骨与学术著作，影响力比其它人巨大，记得殷先生在批评时政时候对于身处戒严时期有很严厉批评，他说他们能够囚禁我的身体，但是不能囚禁我所传播的道理，这样的话在那个时代说出来，真的需要很大的勇气。而他出生在五四运动发生的民国八年，精神上继承了五四运动的传统，而又比五四运动那时的人士晚了一辈，因此他所面对的时代又有所不同，1950到1960年代，那时大陆是共产党统治、台湾则是国民党统治，事实上自由都很难获得保障，另一方面由于国共内战，中华民国政府撤守台湾之后，对大陆许多的热、冷战、斗争都使得我们宪法所保证的自由受到严厉的限制，在那时期，不光是有临时条款，将中央政府组织作很大变动，戒严也把宪法第7至22条所保证的自由与权利限制得体无完肤，当然对于主张自由的知识分子就有很多的限制，乃至于打压与迫害，那段时期使许多人都不堪回首。<br />
<br />
　　在这里又想到，殷先生过世后，殷师母就出国了，几年后她回来了，有机会在一次聚会中碰到她，当时我是台北市长兼任中国国民党副主席，坐在殷师母身旁，我特别站起来对她说，我不是国民党主席，但是今天我要代表国民党向殷师母鞠个躬，表示我最诚挚的歉意，以前我没有身份讲这个话，鞠躬也没用，但是我现在又不一样了，我是中华民国总统，我很希望藉这个机会也向殷师母以及所有当年曾经为自由民主奋斗牺牲的先进表达我最诚挚的歉意。<br />
<br />
　　我知道殷先生家人都是基督徒，殷师母那时跟我讲，事情过去了，但是在我们来看，这段遭遇对殷先生来讲是很不公平的，他承受很多委屈，而他所受的委屈无法依照戒严时期不当审判的补偿条例，这些过程正好是反面教材，在我们开启自由民主新时代时可以记住这些反面教训。也因为这样，殷先生所住过的那栋宿舍，在我担任市长时即指定为市定古迹，通常古迹应该有建筑上的价值，那栋日式房子虽然古迹意义不是很高，但是所有委员一致认为，住过的人对台湾的贡献很大，从这个角度去介定保存的价值。<br />
<br />
　　各位都知道我上任后，大力改善两岸关系，一方面当然两岸经济已发展到必需有更灵活及宏观思考的时候，另一方面也是希望两岸不要再有战争。因为一旦内战，任何不该做的事情都会被合理化，这点我感受很深刻。很多时候到了战争发生时，会变成身不由主，有时明明知道不该做，但是又不得不做，西方有一句名谚：All is fair in love and war.很简单的一句话，就是说在这两个领域中，任何事都是合理的。消极来看就是要创造一个没有战争的海峡两岸，但是这绝不是意谓要牺牲台湾的尊严与中华民国主权，这是我们要誓死捍卫的，但是仍然可以在这样的情况下找到双方达到和平的共同点。<br />
<br />
　　但是光这样消极的避免战争，仍不是我们努力的方向，还有一点就是我在竞选中提出来的第二阶段的民主改革，我去年3月22日当选时，美国总统布什拍来贺电，除了称赞台湾的民主选举，也特别提到台湾是亚洲及世界的民主灯塔，我看了后几乎热泪夺眶而出，五十年代我们也是追求自由民主，但是离开台湾，相信的人不多，现在美国总统如此说，回首前程大家可说感慨万千。另一方面，我也觉得有点汗颜，真的是这样吗？我们的民主真的成熟了吗？我们是不是还需要很多进一步的努力。<br />
<br />
　　我提出第二阶段民主改革，尽管我们在之前已经完成了解除戒严、开放组党，然后解除报禁、国会全面改选、总统直选以及两次政党轮替、政权和平移转，在许多先进国家看来难能可贵。美国的作家写了一本书叫做自由的未来，他提出自由主义的民主，以及非自由主义的民主，他说全世界120几个国家都定期举办选举，但并非每一个国家都合乎自由主义民主的标准，他在书末讲了一句颇富哲理的话，他说在20世纪初期，美国总统威尔逊曾说过，Lets make the world safe for democracy，现在我们要说Lets make democracy safe for the world。也就是说太多形式的民主而没有实质的民主，不但危险，而且也是种灾难。他举出一些国家靠行政命令治国、靠种族主义压迫异己，一样地号称民主国家，但是他觉得有许多检讨空间，甚至于他对他所在的美国也有类似的批评。这点给我感觉到很大的冲击，也就是要如何真正落实我们的宪政民主。我觉得很重要的是我们的法治与人权保障，包括自由在内，这部份要获得更多的保障，否则我们只有转型，不见得具有正义，所以转型正义喊得震天价响，不见得后来能达到真正的目的。<br />
<br />
　　我在就职演说中再三地提醒大家，权力使人腐化，绝对的权力使人绝对的腐化，这难道不是我们应该经常牢记在心的吗？也就是说，我们应该在人权的保障、自由的确保等方面，加上宪政主义，让我们的民主有更深刻的发展。例如我在就职演说中强调绝对不容许有任何非法的监听、不容许政治干预司法与新闻自由，我上任之后也特别去注意，因为以前我们常听说调查局监听，所以我要求所有有权监听的单位彻底检讨，至少在调查局及其它单位向法院申请监听票比起过去已减少了百分之七十，其它单位如果还有这个权限，我们也要求不断地检讨。我们相信这样的努力一定会有些效果。<br />
<br />
　　另外，像最近这一年来我们有些司法的大案，我自己是国民党员，很多国民党的乡亲都希望我能办快一点，但是我在怎样的压力下都不会轻易放弃我不干预司法的态度，这些方面也许有人会认为没有魄力，连这个都不敢做，但是对不起，我真的不敢做，因为做了后就是不知伊于壶底，所以不能有第一次，这是我个人基本的信念。<br />
<br />
　　我们也希望一方面能够建立一个干净的政府，同时也鼓励民间社会蓬勃的发展。我在今年五月十四日签署了1966年两个联合国人权保障公约，一个是「公民与政治权利国际公约」 、一个是「经济社会文化权利国际公约」，这两个公约我们政府都在1967年就签署了，可是一直未批准，四年后中华民国就失去了联合国代表权，这么多年来都没有人去想到，到了民进党执政时提到院会来，当时我是台北市长，看了很高兴，认为我们必须赶快通过，总统批准再送到联合国存放，依联合国规定存放三个月后就生效，当然我们要求存放联合国也不一定会接受，最近我们试过了，联合国也未接受。但是不要紧，我们在立法院通过时，就已经成为中华民国国内法，我也要求法务部在两年之内，检讨所有的现行法规，让它符合这两个公约的标准，希望法务部在今年12月10日国际人权日时能提出检讨报告，这也是我刚讲的第二阶段民主改革的意义，也就是说不仅有民主的型式，有选举、投票、国会，但是否做到我们所谓的自由主义民主，还需要更进一步努力。我也希望在我担任总统时能尽量来落实，建立一个可大可久的宏规，让这些理念在法律里找到根，就是因为这样我才很积极进行，对很多人来讲这可能是不急之务，为何在金融海啸之际还着眼人权，可是愈是这样，我觉得愈要重视这件事情，让它可以在我们社会上生根。<br />
<br />
　　今天在台湾，真的很感谢台湾海峡，让我们有这样的环境可以好好发展自己的制度，炎黄子孙五千年来还没有出现一个自由主义的民主，也许在这个岛上有这个机会，而如果我们在此成功的话，对整个华人社会所产生的影响是很巨大的，这点也是我担任总统之前就一直有的想法。<br />
<br />
　　我们真的要做亚洲与世界的民主灯塔，还有很长一段路要走，但是如果不从现在开始起步，我们永远走不到。我们也许要花很多年，但是如果我们现在开始起步，就有机会看到我们走在这条路上。谢谢主办单位的努力，来举办启迪很多后进的研讨会，希望在21世纪的台湾，可以挥别五、六十年代的威权，迈向一个真正自由民主的社会，我真的很希望我们下一代，当我们说我们来自一个自由民主的台湾时，每一个人都会点头，而不是讪笑，这是我的期望。</div>

							
							
							
							
