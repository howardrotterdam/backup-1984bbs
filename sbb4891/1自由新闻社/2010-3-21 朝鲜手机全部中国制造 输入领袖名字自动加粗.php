　　2004年4月22日中午时分，朝鲜平安北道龙川郡发生了一起严重的火车爆炸事故，导致近200人死亡，1500多人受伤，另有8000多幢房屋被毁。有分析认为，这次造成灾难性后果的朝鲜火车爆炸案，是一次针对朝鲜领导人金正日的暗杀企图。4月22日龙川爆炸事故时，有线索表明不良分子使用了手机，唯恐内部情报外泄，所以手机业务被停了。<br />
<br />
　　停止移动电话服务举措的命令由朝鲜国防委员会直接下达，特别是在权力机关或特殊行业就职的人员使用手机受到了严格的限制，原先持有的手机也被没收。朝鲜在境内全面禁止使用手机之后，花大笔钱购置手机的居民大为不满，因为1台手机机身和入网费共约1300美元，在一夜之间就成了废品。<br />
<br />
　　当时朝鲜的普通工人月均工资为2500朝元左右，一美元兑换1200至1300朝元，这是笔巨大的费用，相当于600多个月的工资。当时人民保安省的官员没收了近万部公司和个人用户的手机。官方给出的理由是，鉴于手机用户激增，当局难以设法监听和控制人们的谈话。至此，只有朝鲜的高层和军方人士，以及在朝鲜的外国人士才有权使用手机。<br />
<br />
　　去年3月，长达5年多的手机使用禁令终于被打破。一些刚买到手机的民众兴奋不已，马上急切地打起了手机，还有的人欣喜地研究着手机的各种功能。一些朝鲜民众表示，政府允许老百姓使用手机，会给他们的生活带来很大的变化。预计到今年初，朝鲜的手机用户将达到12万，占朝鲜总人口数的两百分之一。<br />
<br />
　　朝鲜手机的一大特色就是“国际化”：手机中国造，网络埃及绕。2008年12月15日，由埃及运营商奥拉斯考姆电信和朝鲜政府分别出资75%和25%，合资成立高丽电信，并通过W-CDMA的方式来提供第三代手机服务，可以在平壤市内通话，也可以手机上朝鲜官方的“黎明网”，输入用户名和密码之后，可以听歌、看新闻，甚至是在线聊天。<br />
<br />
　　朝鲜的手机全部来自中国。一位自称所在公司为朝鲜设计手机系统的人士说，“按照朝鲜方面客户的要求”，手机输入法中的“金正日”这个名字的字体在显示屏上要黑体，而且加粗。“日程表模块的同事被要求除两个节日外删掉其他所有的节日，这两个节就是‘大金、小金’的生日。”<br />
