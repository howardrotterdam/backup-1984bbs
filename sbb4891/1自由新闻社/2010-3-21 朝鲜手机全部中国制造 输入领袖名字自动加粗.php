<br />
当时朝鲜的普通工人月均工资为2500朝元左右，一美元兑换1200至1300朝元，这是笔巨大的费用，相当于600多个月的工资。当时人民保安省的官员没收了近万部公司和个人用户的手机。官方给出的理由是，鉴于手机用户激增，当局难以设法监听和控制人们的谈话。至此，只有朝鲜的高层和军方人士，以及在朝鲜的外国人士才有权使用手机。<br />
<br />
去年3月，长达5年多的手机使用禁令终于被打破。一些刚买到手机的民众兴奋不已，马上急切地打起了手机，还有的人欣喜地研究着手机的各种功能。一些朝鲜民众表示，政府允许老百姓使用手机，会给他们的生活带来很大的变化。预计到今年初，朝鲜的手机用户将达到12万，占朝鲜总人口数的两百分之一。<br />
<br />
