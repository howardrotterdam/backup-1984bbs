<br />
2004年4月22日中午时分，朝鲜平安北道龙川郡发生了一起严重的火车爆炸事故，导致近200人死亡，1500多人受伤，另有8000多幢房屋被毁。有分析认为，这次造成灾难性后果的朝鲜火车爆炸案，是一次针对朝鲜领导人金正日的暗杀企图。4月22日龙川爆炸事故时，有线索表明不良分子使用了手机，唯恐内部情报外泄，所以手机业务被停了。<br />
