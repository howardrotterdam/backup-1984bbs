													<h2>中国政治人物的画海沉浮</h2>
						
						
																											<div id="postmessage_266456" class="t_msgfont">中国政治人物的画海沉浮<br />
记者： 张楠<br />
北京<br />
*Sep 29, 2009*<br />
<br />
在中国，政治人物不仅沉浮于宦海，也在绘画作品和新闻图片中载沉载浮、时隐时现。一<br />
个位高权重的人[image: 恢复原貌后的油画《开国大典》最近在北京展出]<br />
恢复原貌后的油画《开国大典》最近在北京展出<br />
物，既可以成为艺术表现的对象，也可能由于失势而从作品中消失。油画《开国大典》就<br />
经历了这样一个领袖人物被反复删改的过程。<br />
<br />
*刘少奇和高岗曾被删除*<br />
<br />
画家董希文1953年创作的油画《开国大典》，描绘了1949年10月1号在北京天安门广场举<br />
行的中华人民共和国成立的盛大庆典。画面中，毛泽东主席在发表讲话，他身后站立着其<br />
他主要的国家领导人。<br />
<br />
然而1955年，高岗、饶漱石反党联盟被揭露。有关部门要求作者对此画进行修改，将高岗<br />
去掉。“文革”开始后，国家主席刘少奇成了“党内最大走资本主义道路的当权派”而被打倒<br />
。这时，上级又要求董希文将《开国大典》中的刘少奇删去。<br />
<br />
1976年，中国政治形势突变，文化大革命结束了。1978年，有关部门决定让画家阎振铎，<br />
在靳尚谊等人复[image: 照片中的刘少奇被删去了]<br />
照片中的刘少奇被删去了制的《开国大典》的基础上恢复董画的原貌，也就是，补上刘少<br />
奇和高岗二人。<br />
<br />
阎振铎对美国之音说：“当时主持中央工作会议的当然是邓小平了，他们就是决定。革博(<br />
中国革命博物馆)得到的信息就是恢复历史的真实。开国大典当时应该是这些人，应该是国<br />
家的主要领导人嘛，要恢复这个。”<br />
<br />
*老画家没有画过想画的作品*<br />
<br />
最近，在“ 新中国美术60周年”画展上，这幅恢复了原貌的复制品再次出现在中国美术馆。<br />
遗憾的是，董希文已于1973年过世。<br />
<br />
那么，董希文本人对自己的作品被反复修改做何感想呢？董希文的学生阎振铎说，他没有<br />
听董教授讲过，可是有一句话令他感触很深。<br />
<br />
阎振铎说：“他病重期间，有位同学去看他。当时他说了一句话，就是‘我这一生中啊，没<br />
有画一张自己想画的作品’。我觉得他这句话本身呢，这里面的含义就太多了，就说明他<br />
的艺术创作受到各方面的干扰。《开国大典》那是非常有独创性的、非常伟大的一个作品<br />
，最后的命运也是这种状态。”<br />
<br />
[image: 这张《开国大典》照片中的董必武不见了]这张《开国大典》照片中的董必武不见了*‘<br />
三突出’修掉董必武*<br />
<br />
在艺术为政治服务的年代，任意修改作品的现象，不仅存在于绘画中，在新闻图片中更是<br />
屡见不鲜。艺术家张大力收集了155幅由于不同原因而被修改过的照片。有意思的是，其<br />
中也有一个反映开国大典的图片。<br />
<br />
张大力说：“《开国大典》，董必武在毛主席的后面。后来这个出明信片发表的时候，(董<br />
必武)给修掉了。他可能想突出(毛泽东)这个主要人物。因为当时有这个文化政策，它是‘三<br />
突出’嘛。他觉得可能毛主席身边有个人突然伸出个头来不好，或者是想强调主席在开国<br />
大典上的他独一无二的镜头。”<br />
<br />
所谓“三突出”是“文革”中提出来的，内容是：在所有人物中突出正面人物； 在正面人物中<br />
突出英雄人物；在英雄人物中突出主要英雄人物。<br />
<br />
*图片修改并不罕见*[image: 张大力的作品《第二历史》]张大力的作品《第二历史》<br />
<br />
张大力说，有些图片被修改了三四次，有一张在延安机场的照片，本来里面有六、七个人<br />
，最早把博古修掉了，后来把彭德怀修掉了，再后来又把刘少奇修掉了。就剩下三个人：<br />
周恩来、毛泽东、朱德。<br />
<br />
张大力说：“真的，我被吓了一跳，我很震惊。这么多东西都被修过。你在一张图片上把<br />
这人去掉了，就等于把一个人杀了一样。杀了不过碗大个疤，可是你在历史上把一个人抹<br />
掉了，那绝对是不应该。”<br />
<br />
[image: 雷锋背后的干树枝被改成松树]雷锋背后的干树枝被改成松树<br />
有些照片则被增加了东西。比如，有一张解放军战士雷锋手持冲锋枪的著名照片，出版者<br />
把后面的背景改了。<br />
<br />
张大力说：“冬天的，他的后面本来是一些草(实为干树枝)，但是这张照片最后把他后面画<br />
成松树了，雷锋站在一棵松树的前面。这样也是因为反映英雄他的高大。”<br />
<br />
《送子参军》是沙飞的一幅摄影作品，它在后来发表时被修改，是因为照片上那位年轻人<br />
参加的不是共产党部队，而是国军。<br />
<br />
张大力说：“当时他去拍那个照片：有个青年人戴个瓜皮帽，他是很高兴地拿了个花，前<br />
面有个驴和他的妻子，还有他爸爸笑去送他。这个照片，当时这个驴上有个旗子，他这个<br />
兵不是参加八路军和共产党。它写的是‘响应蒋司令员的号召’。”<br />
<br />
[image: 这幅照片先是被删去了其他人只保留了毛泽东，后来又被删去毛泽东，改成风景照]<br />
这幅照片先是被删去了其他人只保留了毛泽东，后来又被删去毛泽东，改成风景照*毛泽东<br />
也被去掉了*<br />
<br />
毛泽东的地位也不是绝对不可动摇的。张大力说，有一个画册的出版者想表现延安过去的<br />
风景。由于延安宝塔山下的景色现在变化很大，旧景无法再现，就用了一张老照片，但是<br />
画面上的毛泽东挡了风景，他们就把毛去掉了，改成了一张单纯的风景照。<br />
<br />
张大力给自己收集到的此类照片出了个相集，起名叫《第二历史》。他说：“所以我做这<br />
个作品，我觉得，是非常重要，就是我们曾经经历过什么，我们都应该把它原原本本地、<br />
实实在在地给反映出来，让我们今天看，我们过去是怎么来的，我们为什么今天这样。我<br />
觉得一个民族或者一个国家，它过去犯的所有的错误，或者它过去所有的耻辱，就不能掩<br />
盖。孟子说，‘知耻近乎勇’。”<br />
<br />
张大力称他敢做这部作品，他能去调查、去找这些资料，都说明中国已经进入了一个新的<br />
历史时期。同时，他也指出，在世界很多国家都展览过、也出版过的这部作品，在中国还<br />
不能展览、也不能出版。</div>

							
							
							
							
