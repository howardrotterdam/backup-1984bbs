													<h2>龙应台谈禁书：准备被大陆读者“万箭穿心”</h2>
						
						
																											<div id="postmessage_262147" class="t_msgfont">你所不知道的一九四九――对话龙应台<br />
作者：南方人物周刊记者 黄广明 实习记者 翁倩 发自香港 <br />
<br />
为什么要向失败者致敬<br />
<br />
人物周刊：这本书的主题在一开始就指明了，是向失败者致敬。这个失败者是从什么角度来说的？又为什么要致敬？<br />
<br />
龙应台：一开始，我只是带着一个模糊的、想要认识1949的概念开始入手，并不知道要找什么、能找到什么、找到以后会从哪里出来、出不出得来。一直写到最后，写到95%、98%，才有了这个核心的概念。这是我一直在摸索，到最后出现的东西。<br />
<br />
人物周刊：创作之初你完全没想到会铺得这么开？<br />
<br />
龙应台：这次写作算是一个探险之旅吧。<br />
<br />
人物周刊：你所说的“失败者”，就是“所有被时代践踏、侮辱、伤害的人”？<br />
<br />
龙应台：原来出发的时候，我想象书的内容主要是以我父母为主的外省人的故事，他们那一代失败的国军。<br />
<br />
一开始写，我马上就发现，我想要了解那200万人到台湾后受到的撞击，不可能只了解他们那一边，因为在地有600万人。<br />
<br />
这600万人，之前50年的时间，所受的全部教育、所处的全部环境都是以为天皇战死为荣的，他们这样经历了几代，突然进来了200万人，也许这些人对600万本地人的撞击和改变更大。<br />
<br />
哪怕是现在，有难民来到自己的地方，反对的声音、烧杀掳掠到处都在发生。可是从1945到1949，我所了解的主流历史叙述中，这样的事只有一件，就是1947年的“二・二八”。为了了解那年到底发生了什么事，我就决定往前走，往前走才能真正了解1949。这就决定了这本书后来的走向。<br />
<br />
写到最后，我想，涵盖了这200万人和600万人共同命运的，到底是什么呢？向流离者致敬或向战败者致敬，都只涵盖了一部分。到最后，才发展出扉页上所写的主题――“向所有被时代践踏、侮辱、伤害的人致敬”，也就是向“失败者”致敬。<br />
<br />
那200万到台湾的人是被内战这个机器绞出来的失败者；台湾的20万子弟被送到南洋，是为日本那个国家机器打仗、体无完肤的失败者；台湾本地的600万人过了50年没有选择的殖民生活，当然也是失败者。<br />
<br />
为什么说致敬？以我的家族史来说，我们这一代人，除了老大在大陆以外，在台湾生长的这4个兄弟姐妹中（其中一个是在海南岛逃难时所生，在台湾长大），有3个博士和一个大学生，现在一个是土木工程师、一个是医生、一个是我，还有一个是医院院长。以我们的父母亲60年前的状况来说，母亲在高雄满是难民的码头上岸，找不到丈夫，带着个婴儿，以在菜市场摆个摊子为起点，到60年后子女都有所成就，你想想看，她做了些什么事情、是怎么做到的？你不能不致敬！我致敬是以像我这样的家庭为出发点。<br />
<br />
你再看台湾整个社会的发展，可以想象，如果不是在60年前，他们被战争机器碾压、失败了的话，他们也许不会有机会去思考价值问题，可能还在走原来那个国家大、集体大、胜利大的老路。正是因为在那一套价值里他们整个被打垮了，在后来的60年里，才会追求另外一套价值、另外一个方向，朝一个更柔软、更个人、更文明的方向走。从这个角度来看，他们当初失败了我反而更高兴。这是我写到最后才慢慢浮现出主题的过程。<br />
<br />
这本书，写的是“万骨枯”<br />
<br />
人物周刊：你把焦点放在普通人身上，是如何起意的呢？<br />
<br />
龙应台：完全是探险探出来的。2008年7月15日，美国斯坦福大学胡佛研究院公开蒋介石日记1949年前后的片段。8月1日，我飞到胡佛研究院，像小学生一样跟着一群历史学者每天去图书馆读那些日记。看了两个礼拜之后，我决定，我的主角不是大人物而是小人物。有了那样模糊的想法之后，慢慢地走，最后越来越清楚。我写的台籍日本兵都是没名没姓的，我在丛林里走着，有了那种“一将功成万骨枯”的感受。很多人写“一将功成”，这很重要，但这本书，写的是“万骨枯”。写完这本书后我特别平静，我感觉这是我用文学上的一炷香，为那些60年前被时代践踏、曝尸于荒野的人上的一炷香。<br />
<br />
人物周刊：1949年对你们的家庭影响最大的是什么？<br />
<br />
龙应台：槐生和美君两个人一夜之间脱离了原来的社会网络，连根拔起被丢到一个空的地方，这对我们的影响是很大的。我们小时候就知道，你跟身边所有的人都不一样，上学时我就是那1比59，那59个孩子都有很大的家族，逢年过节会去祭祖啦逛庙会啦，清明节有墓可扫。我们是难民的孩子，从小就知道自己跟人家不一样。这200万人有一个共同点：他们的全部投资都给了孩子的教育，对这200万人，教育就是垂到深井、能让你爬出来的惟一一根绳子。<br />
<br />
人物周刊：你的书主要写了两种人的痛。一种是大陆去台湾的人，经历了颠沛流离骨肉分离的痛，比较好理解，另一种是台湾本省人的痛，可否具体谈谈后一种痛?<br />
<br />
龙应台：现在台湾网络上已经有了一种说法，自以为是蓝营的人说，“我以前对于深绿的痛苦不了解。”这就是这本书的疗伤和解作用。探险的时候，去访问屏东乡下的老人时，我才发现他们的痛。十七八岁的时候，万分荣耀地穿上军服效忠天皇了。像书里提到的两个人蔡新宗和柯景星，学习怎么打耳光、监护俘虏营、虐待战俘。很多人为日本死在战场上。日本战败，这些人中活着的成了战犯。26个死刑，100多个10年、20年徒刑。<br />
<br />
台湾归还中国，那台湾到底是战败还是战胜？他们懵了，不知道了。这是我在做这本书时的最大震撼。刚才说到的那两个人，被当作战犯关了10年，回到台湾时，已经改朝换代了。他们不会说国语不会写汉字，整个社会以你为耻，你还不知道怎么回事。从此之后连对你的儿女述说都不可以。这一辈台湾人的痛苦超过我的父辈，是双重的。吊诡的是他回到了故乡，可他是流亡的。所以他们绝对地沉默了。<br />
<br />
一个帮我做校对的朋友，25岁，校对完眼泪汪汪地对我说，读这本书让她想起了祖父，她祖父常常喝酒，和一帮白头发的老人喝了酒就大唱日本军歌，她说我当时都觉得很羞耻，看完这本书后她很震撼。所以我觉得，这一段历史大陆不能不了解，否则两岸怎么可能和平？<br />
<br />
人物周刊：你把外省来的国民党士兵和本省为日本打仗的台湾兵相提并论，不怕有的读者，特别是大陆读者情感上接受不了？<br />
<br />
龙应台：在台湾本地我也担心会有一些反弹，所谓深蓝的人会受不了我对深绿的同情，深绿的人受不了我对新四军的同情。但现在书已经出来半个月了，这样的情况并未出现。这本书反而让原本敌对的双方加深了了解，进而和解。<br />
<br />
由于我缺乏在大陆生活的经验，以前我的书都是在大陆出版之后，我才知道，原来大陆读者也这么喜欢。而这本书，从头到尾，大陆读者都在我的心里。我写东西时心里有个自觉，就是有什么东西我希望大陆读者知道，虽然台湾读者一直是我的第一读者。<br />
<br />
其实这本《大江大海》在大陆出版可以有一个副题，就叫《你可能不知道的台湾》。在写的时候，我知道，我在为你写你不知道的台湾。同时我也知道，国共内战的那个部分，会刺伤大陆读者，因为以前他们从未有过这样的认知，我所呈现出来的一部分他们不习惯。<br />
<br />
但这层还不是最难的，最难的是民族的界限――中国和日本的界限，就触及到他们的底线。但我不能不去碰，我也希望大陆的读者接受我的底线，看看自己有没有更宽阔的视野来重新认识这些事情。在这个过程中，我免不了会挨骂，甚至“万箭穿心”。<br />
<br />
知道敌人痛在哪里，你就拿不起枪来<br />
<br />
人物周刊：你以一种普遍的人道关怀，对抗日的国民党士兵、打内战的国民党士兵，乃至实施侵略战争的日本士兵、为日本打仗的台湾兵都一视同仁，这么有挑战性的写作，你的信心来自于什么地方？<br />
<br />
龙应台：现在想要把我五马分尸的人，是因为看得不够多，如果他看得够多，我相信他慈悲的心不见得比我少。我更相信，如果你有机会看到敌人的伤处，就不会有战争。如果你知道你的敌人痛在哪里，你就拿不起枪来。这本书就是让国军和解放军都能看到对方的痛，本省人和外省人也能看到对方的痛。<br />
<br />
这本书还没出版的时候，日本《朝日新闻》的记者来采访我，我先把和日本有关的部分给他看了，日本记者的反应是，这本书一定要有日文版。因为我写的这个部分正好是日本人的“黑盒子”。<br />
<br />
第一，日本人是不愿面对1945的，正如国军不愿面对1949。所以1945的前前后后，都是美国学者在写、中国学者在写。<br />
<br />
第二，我描述的台湾日本兵的遭遇，都是殖民者带给他们的，但他们并未得到和日本兵同样的国家赔偿，或者得到的赔偿是日本兵的两百分之一。所以到现在为止，台湾兵还在上诉。日本政府的回应是，《国际法》规定战败之后你就是中国人了，我对你没任何义务。他们受到的还不止双重伤害，而是三重伤害。所以我也希望日本人能认识到台湾人的伤、大陆人的伤。<br />
<br />
有人问我，如果这本书不叫《一九四九》，还可以叫什么？我说，那就叫《战争与和平》吧。<br />
<br />
人物周刊：所以你是站在人类普遍情感中来说这些事情。<br />
<br />
龙应台：对，如果你是大陆人，你满18岁的时候正好是1937年抗战爆发，你就会投笔从戎变成后来的李维恂。如果你是台湾少年，你18岁的时候刚好是1942年，你就变成日本兵。如果你18岁的时候刚好是1945年，你在台湾，你就变成国军。你说他们的命运有差别吗？完全没有。<br />
<br />
人物周刊：他们当年的可悲在于无法主宰自己的命运，面对强大的国家机器时没得选择，而今天的年轻人是可以有所选择的。<br />
<br />
龙应台：对，但我也不是说他们完全没有选择。我书里讲到1991年海湾战争的德国士兵，就是不愿意德国参战而离开了军营。一个国家的整体，往哪个方向走，跟里边的螺丝钉怎么思考是有关系的。我一点不愿意说教，但我希望读者能够领会，这跟你太有关系了。你要做选择，你要思考。<br />
<br />
人物周刊：历史不可假设，但我们还是不妨一问，60多年前那场兄弟阋墙的战争必须打吗？没有办法避免吗？<br />
<br />
龙应台：这恰恰不是我该回答的问题，这是历史学家回答的问题。<br />
<br />
人物周刊：今年是中华人民共和国建国60周年，你通过这本书，想对大陆政府说些什么？<br />
<br />
龙应台：我反而觉得最重要的是对大陆的人民――我的读者说。我希望大家来读这本书，因为只有当你了解了对方的痛苦，才能对一些行为、一些思维有新的认识。你再作的价值判断，才能够更接近真实。另外，我觉得要写出大陆人的痛苦，需要一万本这样的书。当我说大陆人不知道台湾人痛处的时候，台湾人同样不知道大陆人的痛处。只有两岸人民对对方有了真正的了解，才会有真正的和平。如果两岸人民充满误解和不理解，即使两岸领导人作出什么和平举动也不能持久。<br />
<br />
另外，我很想说的是：过了60年，我们――包括大陆，要跳出军事史的单一眼光，必须用文明史的眼光来看这60年。<br />
<br />
文明最核心的意义，就是人的价值。如果用文明史的眼光来看的话，从各条路线逃亡到台湾来的200万人，加上本地的经过50年殖民历史的这600万人，这两股在战争中失败的人在一起，变成今天这个以文明的价值、和平的价值为核心思维的一个团体、一个岛屿。经过这60年的转型，战争史上的失败者，在文明史上是成功者。这种转换，你名之为神奇都不为过。<br />
<br />
我非常非常以我的失败者上一代为荣。<br />
<br />
（龙应台女士为台湾居民，文中的一些称谓保留了她的习惯说法，不代表本刊立场）</div>

							
							
							
							
