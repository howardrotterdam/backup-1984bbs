*拥抱人性--以小人物的生命历程诠释历史*<br />
<br />
两位80岁的台湾国军老兵在接受龙应台采访时，用苍老的声音唱出他们60多年前学唱的《<br />
三大纪律八项注意》。<br />
<br />
这两位老人是龙应台为撰写《大江大海--一九四九》一书而采访的成千上万的受访者之一。1945<br />
年日本战败，台湾一片荒芜，这两名在台东土生土长的17岁青年参加了国军。他们被一艘<br />
大船运到山东打内战，在国共内战的激战中跟无数国军士兵一起被俘后立即编入共产党军<br />
队，掉转枪口打国军，后又入朝参加韩战，被美军俘虏后，两名台湾青年又被命运的抛物<br />
线离奇地抛回台湾。<br />
<br />
龙应台说，她的新作《大江大海--一九四九》就是要通过60年前她的家族故事、以及无数个<br />
被那场残酷的国共内战“绞肉机”甩出来的个体的生命历程。<br />
<br />
[image: 台湾知名作家龙应台]台湾知名作家龙应台<br />
龙应台表示，希望她的读者在掩卷之余思索一个问题，那就是一个人在国家、在集体、在<br />
一个巨大机器里的位置以及个人对其走向所能发挥的影响，这就是她写此书的意图。<br />
<br />
她说：“我要做得比较单纯，我只是想把不管是苏联红军、德军、日本军、中国军--国军或<br />
者是解放军，我想要把每一个机器，集体的机器，把它拆解开来，让你看到里面那个孤零<br />
零的个人，而且让你看到，那个孤零零的个人他在流血、他在疼痛的地方。”<br />
<br />
*一九四九--两德分裂、两岸分裂的年头*<br />
<br />
龙应台新书《大江大海---一九四九》上个月面世正值中国在为欢庆建国60周年做准备，因此<br />
她的新书格外引人关注。龙应台说，大陆方面以及国际媒体的对此书反应令她惊讶。<br />
<br />
她说：“我发现他们问的第一句话就是说，你这本书是不是冲着10月1号来的。我吓一大跳<br />
，就是说是不是冲着建国大业来的，我非常惊讶，因为在过去的一年400天之中，我连报<br />
纸都很少看，没有时间看，心目中也不会想到10月1号或者是10月10号是什么日子，更不<br />
要说是冲着10月1号来了。”<br />
<br />
龙应台说，出书确实有时间的巧合，但决不是此书目的。她写此书最初是在1989年柏林墙<br />
倒塌时起的念头，但一直到1999之后的几年里，父亲过世，母亲失忆，才让龙应台有了紧<br />
迫感，担心历史记忆的大门一旦关闭就永远敲不开，于是开始动笔。<br />
<br />
*历史不仅是胜利者的 更不会是当权者的*[image: 龙应台在台北新书演讲会上]龙应台在台北新<br />
书演讲会上<br />
<br />
当龙应台为收集史料奔走于台湾各个档案馆和台湾国防部各个机构时才发现有关1949年的<br />
资料如此之残缺不全。<br />
<br />
她说；“因为当时是溃散来台，因为是有一个集体战败心理，所以，历史都不是战败者在<br />
说，你没有时间，没有心情，也不忍去面对，结果就是，战胜者就会完整地在讲故事，他<br />
把你们的故事也讲进去了，所以当你在讲内战历史的时候，几乎都只有大陆的叙述。”<br />
<br />
龙应台说，历史不仅仅是胜利者的，她呼吁台湾民众行动起来，把自己、把长辈的经历，<br />
把60年来一直藏在黑盒子深处的千千万万亡灵的痛苦、伤痛都化成文字，或上网、或出书<br />
。<br />
<br />
与此同时，龙应台说，不能把历史留给官方来写，那样的话，共产党和国民党一边一半对<br />
那段历史的叙述，合起来恐怕就是一个完整的谎言。<br />
<br />
因此，龙应台呼吁，中国大陆作家也行动起来，动手书写大陆民间的历史，书写大陆人民60<br />
年来那许许多多隐忍不言的伤痛。龙应台说，如果能够如此，像《大江大海--一九四九》这<br />
样的书在大陆就不会仅是一本，而是成千上万本。<br />
<br />
*胜利与失败的轮回*<br />
<br />
龙应台表示，她和她的无数读者一样都是怀着对参加了60年前那场国共内战的父老一辈的<br />
感恩之情来撰写、来阅读这本书的。她说，有在国共内战战场上打过仗的长辈气愤地给龙<br />
应台写信，以当年自己的英勇与付出质问龙应台怎么敢把他们称为失败者？<br />
<br />
龙应台说，国民党在60年前的战场上确实失败了，但她要给“失败者”打上引号，重新定义<br />
胜利和失败。<br />
<br />
她说，正是这些失败者来到台湾，白手起家，建立起一个文明的、柔软的、尊重个人自由<br />
与权利的新的价值观，建立起一个现代台湾。因此，龙应台说，她为失败者所取得的成就<br />
而骄傲，以自己是失败者的第二代为荣。<br />
<br />
龙应台说：“他们刚刚好是因为当时军事上的失败使得我的长辈们这一整代人慢慢地在60<br />
年之中，在台湾这个地方逐步发展出另外一套价值，这个价值离60年前那种国家的、集体<br />
的、胜利的、战争的价值越来越远，而离我称之为温柔的力量的价值越来越近。在这个意<br />
义上，当时军事上的失败反而是一种收获。”<br />
<br />
*超越胜败 超越1949*<br />
<br />
一名来自中国上海的读者被《大江大海--一九四九》一书深深触动，他说，他读书时流下眼<br />
泪。不过他认为，台湾更重要的是要超越1949。<br />
<br />
这位上海青年说：“固然我们要回到1949这样一个原点来解读历史，也需要准确、清晰地<br />
来阐读那段历史到底是怎么回事，但是，我觉得对台湾来说最重要的是要超越1949。”<br />
<br />
龙应台的新作由大量史料构成，承载着历史的重量。但她强调说，她只是一名历史的小学<br />
生，无意撰写历史。然而面对历史，龙应台说，“台湾社会只有把过去抽丝剥茧地弄清楚<br />
，并让下一代人认识了解，前面的路才走得更好。”她说，“所有的历史不是为了过去，而<br />
是为了未来”。<br />
<br />
龙应台新作《大江大海--一九四九》2009年9月份分别在台湾、香港出版，现在已经有很多<br />
大陆出版社跟龙应台联系，希望出版此书。但香港方面传来消息说，此书已遭中国大陆的<br />
网络封杀，以便“消除杂音”。龙应台的回应是，这只能更凸显两岸对彼此的不了解。她说<br />
，让中国大陆民众读到这本书非常更重要，因为他们能够以这本书为起点来了解台湾。</div>

							
							
							
							
