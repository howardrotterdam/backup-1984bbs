地位甚至还在居里夫人之上。两人都因为各种原因而放弃了祖国国籍，但都心念故国。居<br />
里夫人本是波兰人，后入了法国籍，但她把自己发现的元素取名为Po，以记念她的祖国波<br />
兰。有意思的是，居里夫人也曾被人捏造事实攻击品德败坏,有生活作风问题，以致她唯一<br />
