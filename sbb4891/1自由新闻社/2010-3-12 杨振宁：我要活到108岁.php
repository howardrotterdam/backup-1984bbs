																											<div id="postmessage_514304" class="t_msgfont">杨振宁：我要活到108岁<br />
<br />
<img src="http://www.zaobao.com/special/face2face/images/face2face100312.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
潘星华 (2010-03-12)<br />
<br />
　　科学家杨振宁，近日到新加坡出席南洋理工大学高等研究所为美国诺贝尔物理奖得主葛尔曼（Murray Gell-Mann）举行80岁生日国际物理学家会议。<br />
　　杨振宁已88岁高龄，他接受本报专访时，以灵活炯炯有神的眼睛，敏锐的思路，还有利索的行动告诉记者说：“我要活到108岁。”<br />
<br />
　　88岁中国人称为“米寿”。杨振宁借用中国学者冯友兰88岁那年写给同龄好友金岳霖的两句话说：“何止于米，相期以茶。”，表明自己不仅以“米寿”知足，还正信心十足向“茶寿”进发。<br />
　　88岁所以是“米寿”，因为“米”字上下两个八字，中间十字，所以是八十八。“茶”字上面二十八，下面八十，两者相加，就是108。<br />
<br />
　　杨振宁（1922年生）比葛尔曼（1929年生）大七岁，葛尔曼与他同行，既是他的好朋友，也是他的竞争者。杨振宁在出席葛尔曼生日晚宴上说：“20年后，新加坡会变成世界人均收入最高的国家之一。到时，南大高等研究所为葛尔曼庆祝100岁生日的时候，我一定会来参加。”届时，杨振宁108岁。<br />
<br />
　　杨振宁去查了资料，了解美国88岁的男人，一般可以再活五年。他说：“我会比他们活得更久，因为现在88岁的男人很多都在生病，我却一点病也没有。到我‘茶岁’的时候，翁帆还很年轻，再过20年，翁帆54岁，还是风韵犹存。当然我明白，要‘相期以茶’，是个奢望。”<br />
<br />
　　杨振宁是香港邵逸夫奖的评审团主席，每年在这个被誉为“东方诺贝尔奖”的颁奖礼上，邵逸夫都亲自出席。相对于今年103岁的邵逸夫来说，88岁的杨振宁只是小弟弟。<br />
<br />
　　杨振宁说：“103岁的邵逸夫，状态还是很好。去年10月，我们刚和他见过面。在颁奖礼的鸡尾酒会上，他先和宾客喝酒，仪式上，他走30米长的路颁奖，之后，再和我们一起吃两个钟头的饭，完全没有问题。每次他看到翁帆，总是很高兴地跟她拉拉手，用英文说‘我们每年见面一次’。” 我有很好的基因<br />
<br />
　　杨振宁谈到自己的身体，他说：“我能这么健康，跟我有很好的基因有关。我也很幸运，没有受到物质上和心理上的极端压力。除了杜致礼（前妻）生病的那几年，我稍微狼狈，这么多年来我一直很好。后来碰到翁帆，她把我照顾得很好。北京清华大学和香港中文大学都为我提供种种做研究的方便，使我日子过得很舒泰。” 他承认自己身体好，也跟现代医药科技发达有关。 他说：“1997年，我在美国做了心脏搭桥手术，很成功。去年我在北京协和医院做MRI（磁共振Magnetic Resonance Imaging），发现12年前心脏搭的四个‘桥’很好。很多人说，心脏搭桥的有效期是10年，过了10年，‘桥’阻塞了，必须重新再‘搭’，我很幸运，没有再搭桥的需要。” <br />
<br />
长寿秘诀：看书<br />
<br />
　　杨振宁认为，一个人要长寿跟心态很有关系，需要对很多事物都感兴趣。<br />
　　他说：“我想，一个喜欢看书的人，不容易老。多看书，可以跟得上时代发展，与时并进，人就显得年轻。我比较多看评论，只要他们推荐什么好书，我会立刻去图书馆借阅，或者请秘书去买。”<br />
　　杨振宁说自己很“懒惰”，并没有经常运动。他只喜欢坐着喝茶看书，偶然会骑一下家里的健身脚车。不过，年轻时候的他，却是很喜欢运动的。 他说：“我喜欢走路，年轻的时候，每到一个城市，我是以步行去认识这个城市。记得有一次在台北，我从台北火车站走去台湾大学，走了一个多小时，这条路是我喜欢的。我很明白，虽然爱走路，年纪大了，却切忌跌倒。现在因为有一条坐骨神经容易碰到脊骨，走多了感觉不舒服，路也就少走了。” 88岁，中国人称为“米寿”。杨振宁在接受本报专访时，借用中国学者冯友兰88岁那年写给同龄好友金岳霖的两句话说：“何止于米，相期以茶”，表明自己不仅以“米寿”知足，还正信心十足向“茶寿”进发。<br />
<br />
《联合早报》<br />
（编辑：罗婉菁）</div>

							
							
							
							
