核心提示：据耶鲁大学网站消息，耶鲁2002届毕业生张磊（音）已经承诺，将向耶鲁大学管理学院（SOM）捐赠888万8888美元。这是到目前为止，耶鲁管理学院毕业生捐赠的最大一笔个人捐款。<br />
