													<h2>【转】从耶鲁毕业的夫妻：张磊捐款内幕</h2>
						
						
																											<div id="postmessage_429774" class="t_msgfont">张磊向耶鲁大学捐款8888888美元、创耶鲁管理学院中国毕业生个人捐款纪录的新闻，一时间在国内石破天惊。中国网友立即对张磊和他创建的 Hillhouse Capital Management (高瓴资本管理有限公司) 展开“人肉搜索”。有人极为愤怒：“中国辛辛苦苦培养的高材生帮着人家发展”，甚至调查出他和他的公司在四川地震等事件中并缺乏表现等等。张磊吃里爬外的形象跃然而出。<br />
<br />
我们夫妻二人都在耶鲁接受的博士教育。读到这则新闻，心里实在非常复杂。老实说，如果我们有张磊的能力，也许确实会优先考虑给国内捐款。几年前耶鲁就有毕业生在《纽约时报》写文章，说自己就能捐那么几十万，给了耶鲁，不过是往满满一大桶水中加一滴而已，无关紧要。但是，如果同样的钱给了非洲，不知道能救多少条人命。你会怎么选择？<br />
<br />
<br />
<br />
　　但是，回忆一下我们自己的经历，又对张磊的行为感到理解。二十多年前我们结婚时，妻子在北京是个“黑户口”。她被分到外地，我们不愿意两地分居，索性 “黑”了。代价是没有工作，有时还为临时户口操心。后来决定出国，两人一起学英语，考托福。1993年我们正处于弹尽粮绝的状态，她接到从耶鲁寄来的一个厚厚信封，打开一看，简直不敢相信自己的眼睛：她被录取了，两万多美元的学费人家给支付了，另外给将近一万的生活费，整个三万多美元！有生以来，我们从来没有见过这么大的钱。<br />
<br />
<br />
<br />
　　可是，接下来的事情就麻烦了。有这笔钱并不一定能出国。出国要有护照。按当时的规矩，大学毕业服务不够一定年限者，出国必须有海外关系，还必须支付大学的“培养费”，把账还清了以后，就可以扫地出门了。于是，我们全家紧急动员，先找到在台湾的姨妈开证明，然后到街道派出所开证明，记不请跑了多少地方，当然也送了不少礼，其中颇有些差点前功尽弃的惊险关节。最后，把所有积蓄都拿出来，按照国家开出的帐目，把大学四年国家在她身上花的钱全都还清。再向父母借了些钱买机票，一下子就飞了过去。半年后，我也跟去探亲。我毕业后为国家服务十年，不用缴纳大学的培养费。但是，我去探亲，按规定必须辞职。而这又是一场有惊无险的奋斗，比如找地方存档案、在一堆“不行”、“不办”的声音中绝处逢生等等。我还记得最后办成的那一刻，跑到单位要最后一个文件。窗口一位冷冰冰的小姐把盖好章的一张纸往我面前一仍，甩过来一句话：“你从此和我们没有关系了！”<br />
<br />
<br />
<br />
　　我到了耶鲁探亲，人家对我这个“家属”则无微不至。比如，我只需缴一点钱就有了医疗保险，白拿了学校图书馆的借书卡，使用健身房等等设施，还能在旁听两门课。总之，除了课松一些外，和正式学生也没有什么太大差别。我正是利用这个机会好好表现，被教授看中，什么也没有考就被录取到硕士课程。日后一帆风顺，直到拿了博士，而且六年下来一直拿着全奖。除了正常的奖学金外，学校还给各种钱在夏天让我学英语、学日文，甚至送我到日本学了整整一年。说“耶鲁改变了我的一生”，难道还有什么争议吗？<br />
<br />
<br />
　　张磊的经验是什么我不得而知。但是，从中国上大学、工作到耶鲁读书，一个人直接的感受往往确实就是“耶鲁改变了我的一生”。张磊的捐款，在耶鲁从校友中拿到的捐款中只是很小的一笔，在美国并没有太多新闻价值。在中国有新闻价值的，是这一行为所显示的教育模式和中国是多么不同。<br />
<br />
<br />
<br />
　　第一，美国的名校，特别是常青藤，现在大多靠校友吃饭。这些学校只要发现人才就去招募、争夺。你要是穷光蛋，学校就把学费生活费全包下来，而且还会毕恭毕敬地说：“感谢你到我们这里来读书！我们的校园因为有了你一定会变得更加丰富。”入学后，学校对你无微不至。特别是本科生，有时让我感到学校活象个惯孩子的父母。比如，大学生是谈恋爱的最佳年龄，中国的大学对待学生的恋爱经常有各种“不准”。美国的学校竭尽全力为此创造条件，甚至在招生中采取倾斜政策，保证男女平衡。一位美国学生告诉我：大学生是第一次离家的孩子，刚离开父母心里空落落，大学就要成为学生的第二个家，迅速填补父母在孩子心里留出来的感情真空。如果你在大学里找到自己的配偶，那是学校最高兴不过的。大学所期望的是：你们夫妻一辈子都忘不了自己的家庭是在哪里组成的，都会把大学当成自己的家。日后家里有需要，你当然会把大笔的捐款拿出来。当然还要把自己的孩子“送回家”读大学。<br />
<br />
<br />
<br />
　 第二，学校靠校友，对毕业生也就非常恭敬。比如，我们毕业后，学校总把校友刊物免费寄来，系主任每年写信报告系里的情况，学校在我们的居住地区有活动总要通知。耶鲁选校董，也每次都把选票寄来，并且反复通过电子邮件等通信手段督促投票。要知道，校董是学校的最高权力机构。校长就是校董事会任命的。谁进董事会，又要由校友投票决定。2002年著名华裔建筑师林璎当选耶鲁校董，就是受到校友协会的支持。我们夫妇当时虽然博士都还没有毕业，但已经有了硕士学位，以校友的身份投了票。这大概是我们作为外国人在美国行使的唯一一次选举权。所以，我们拿的并不仅仅是一张耶鲁的文凭，而且是一个当家作主的权利。学校要是惹你不高兴，你也可以通过校董事会施加压力。<br />
<br />
<br />
<br />
　　张磊究竟对中国捐了多少钱，这是另外一个问题。但是，他给耶鲁捐钱，则不过是人家大学经营模式的日常运转和效率而已。你现在就是给美国名校缴足三万多美元的学费，人家培养你还是赔本的。何况许多学生是人家倒贴钱请来的。这么赔钱培养学生怎么赔得起？人家学校牛就牛在这里：我们的教育能够保证你成功，而且保证你成功之后会认识到是我们的教育改变了你的一生，最后你会捐钱来感恩。如果你毕业后收入低、欠的教育贷款还不起怎么办？许多名校（特别是法学院等）的作法是：全免！理由大致有两条：第一，在我们这么优异的地方毕业后，你放弃高薪而从事低薪的公益事业，那就算我们学校为社会作贡献了。第二，如果你真没有技能拿到高薪工作，那一定是我们教育的失败，对你说“对不起”还来不及，怎么会追着向你要钱呢？<br />
<br />
<br />
<br />
　　张磊的行为，应该促使中国的高校好好想一想。我们要是一天到晚和学生算培养费、惩罚不能按期还贷的学生，怎么指望学生象张磊对耶鲁一样对待自己的母校？</div>

							
							
							
							
