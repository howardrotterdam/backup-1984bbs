													<h2>河南人张磊888.8888万美元捐赠美国耶鲁大学</h2>
						
						
																											<div id="postmessage_426424" class="t_msgfont">2010年01月12日 来源:大河网-大河报<br />
<br />
　　首席记者李钊 <br />
<br />
　　中国人民大学本科毕业、后在美国耶鲁大学就读的张磊，“已经保证”向耶鲁大学捐赠888.8888万美元，并称耶鲁大学改变了其一生。不少中国网友对此事表示不满，认为张磊不该向耶鲁捐款；也有网友反思说，这是中国教育的失败。<br />
<br />
　　昨晚，本报记者独家采访得知，1972年出生的张磊来自我省驻马店，曾是该市高考状元，耶鲁毕业后曾在亚洲开发银行和美国金融界一些有名的机构里工作过。<br />
<br />
　　同桌讲述张磊当班长人缘很好<br />
<br />
　　“捐赠美国耶鲁大学的张磊，是我高中同学。”昨夜9时许，记者几经周折联系到驻马店日报社法制周刊主编张承训，张承训上网看到张磊的照片后，兴奋地喊叫起来:“就是他，我俩高二、高三还是同桌，关系很‘铁’。”<br />
<br />
　　据介绍，张磊是驻马店市驿城区人，父亲是市外贸局干部，家庭条件较好。1986年，张磊以优异的成绩考取驻马店高中。高二，文理科分班，张承训和张磊被分到了107班(文科班)。排座位时，因俩人一样高，班主任老师孟法制将他俩排到了一起。<br />
<br />
　　“当时，驻马店高中是驻马店市成绩最棒的高中。”张承训说:高二开学不到一个月，张磊就被同学们选为班长。当时选班长，是民主投票选举，张磊人缘很好。<br />
<br />
　　说起张磊的人缘，张承训举了3个例子:张磊的爸爸藏书多，张磊经常将书抱到班里，让同学们看；张磊当时家住市区富强路，距学校不到50米，每到周末就带同学到家里，他母亲热情慈祥，经常蒸肉包子给同学们；体育课上，张磊爱组织同学们踢足球，他的传球和射门常常引得同学们喝彩。<br />
<br />
　　“张磊听课很认真。”张承训说:高二、高三同桌两年，张磊是那种当堂学习新知识当堂消化的高效率人。<br />
<br />
　　1989年，张磊以驻马店市文科总分第一名的成绩考取中国人民大学，学的是国际金融专业。张承训说:“我清楚地记得，毕业聚会时，班主任孟法制老师让他讲两句。他说，学习没啥技巧，学的东西课堂上消化就是了。学习对他来说，是一件很轻松的事情。”<br />
<br />
　　班主任回忆张磊最看重的是效率<br />
<br />
　　昨夜10时许，记者终于拨通了张磊高中期间班主任孟老师的电话。“刚才电话一直占线，是张磊的几个当年要好的同学打来的电话。”“张磊是一个出类拔萃的人，取得今天的成绩，我一点也不感到惊讶。”<br />
<br />
　　孟老师说:1986年秋季开学才几天，张磊的特点就显现了出来。他说话语速很快，但吐字清晰，表达概括能力强。同学们选他当班长，高二、高三期间一直没换过班长，一直是张磊管理全班同学。<br />
<br />
　　“在高中期间，他读了很多课外书。”孟老师说，“有一次，见他课桌上放着《资本论》，我好奇地问他，他说已看过两遍了。张磊上高中不是死读书，涉猎面很广。”<br />
<br />
　　孟老师告诉记者:优秀学子都有自己的个性和特色。张磊大学毕业后，每年春节总是让同学们给当年的老师和班主任捎句问候；当年从美国耶鲁大学毕业，他回国创办了一个门户网站。后来门户网站转手之后，他才从事金融服务业。卖网站那年，张磊回驻马店，还请当年的高中老师和部分同学在大酒店里撮了一顿。<br />
<br />
　　知情人说他给耶鲁捐款很正常<br />
<br />
　　11日深夜11时许，张磊的知情人――高中同班同学、同年考取中国人民大学的梁宇(现为国内某银行金融专家)在接受本报记者独家采访时说:“张磊现在正在北京，国内现正在炒作他的这一举动。对这件事，他不愿直接面对媒体的采访。我刚和张磊通过电话，张磊说这其实是一件很正常的事。”<br />
<br />
　　梁宇告知张磊，大河报作为家乡的报纸，对这件事情很关注。张磊委托梁宇转达他的意思说:自己对中国和美国都熟悉，了解中美文化，并深爱着自己的祖国和家乡。在耶鲁管理学院求学，学到了很多东西，不仅仅是金融或企业家精神，还有给予的精神；在华尔街金融界的历练，使自己达到了一种层次。他创办Hillhouse　CapitalMan－agement(高瓴资本管理有限公司)，是以耶鲁的捐赠基金作为初期投资运作起来的，耶鲁在公司初期运作期间投资帮了很大的忙，其资本来源主要是美国；如今事业做大做强了，捐赠耶鲁是件正常的反馈与报答，没有必要进行任何炒作。<br />
<br />
　　我1972年出生，1989年进入中国人民大学学习，我选择的专业是国际金融。当时其实我连国际金融是什么都不知道。我只知道很多优秀学生都在学这个专业。<br />
<br />
　　1998年，我申请到美国的大学读研究生。我选择了耶鲁大学，主要有几方面原因，首先，这是我父母唯一听说过的外国名校。在中国，耶鲁也是人们都知晓并且尊重的名校。这也是我想回馈耶鲁大学的原因之一。耶鲁帮助中国的历史已有100多年了，很多中国领导人曾在耶鲁受过教育。但在很长一段时间以来，这种互助的关系都是单向的，所以我极想改变这一点。<br />
<br />
　　耶鲁管理学院改变了我的一生，这一点也不夸张。在耶鲁上学的时候，我离开过一年并回国创办一个网络公司。后来网络泡沫破灭，我又回到了学校。我曾经在耶鲁投资办公室和华盛顿新兴市场管理机构工作过，这些经历对我来说都是无价之宝。我在2005年又回到了中国，开始了我自己的投资基金。我们以3000万美元起家，而现在我们的总资产已经达到25亿。我创办的高瓴资本管理有限公司(据称负责管理耶鲁大学基金在中国的投资)现在已经成为中国最大的独立基金。　注:自述有删节<br />
<br />
　　张磊自述我为什么捐钱给耶鲁<br />
<br />
　　据悉，张磊还是耶鲁大学北京校友会副会长。</div>

							
							
							
							
