<br />
耶鲁大学网站有关此消息的报道<br />
环球时报1月8日报道&nbsp;&nbsp;据耶鲁大学公共事务办公室网站1月4日消息，美国耶鲁大学校长理查德・莱文教授宣布，耶鲁2002届毕业生张磊（音）已经承诺，将向耶鲁大学管理学院（SOM）捐赠888万8888美元。这是到目前为止，耶鲁管理学院毕业生捐赠的最大一笔个人捐款。<br />
<br />
报道说，张磊是Hillhouse Capital Management（高瓴资本管理有限公司）创建人，这笔捐款将主要用于耶鲁管理学院新校区的建设。另外一部分作为奖学金，提供给“杰克逊全球事务学院”国际关系计划项目，以及资助耶鲁各种和中国有关的活动。<br />
<br />
<br />
报道说，1月4日，莱文和耶鲁管理学院院长Sharon M. Oster 在北京朝阳区威斯汀酒店共同主持一个特别活动时，宣布了这一消息。在这次活动上，多名耶鲁管理学院优秀校友还进行了小组讨论，主题是“在中央王国投资”。<br />
<br />
报道说，张磊之所以选择捐赠8,888,888美元，是因为数字8在中国文化有特别含义，其发音类似“发”，代表了财富和运气。<br />
<br />
报道说，张磊在中国中部长大，曾是高考状元。他曾在中国人民大学学习国际金融，后来到耶鲁读MBA，并获得国际关系硕士学位。张磊说，耶鲁改变了他的一生，他创建的Hillhouse Capital Management就是以横贯耶鲁管理学院的一条道路命名的。</div>

							
							
							
							
