													<h2>徐友渔呼吁将诺贝尔和平奖授予刘晓波</h2>
						
						
																											<div id="postmessage_842723" class="t_msgfont">继前捷克总统哈维尔之后，中国社会科学院退休政治哲学教授、中国著名的自由派代表人物徐友渔日前也向诺贝尔和平奖评审委员会发出呼吁：将本年度诺贝尔和平奖授予刘晓波。<br />
<br />
9月24日，中国知名的公共知识分子、中国自由派代表人物徐友渔教授在哈尔维之后，向诺贝尔和平奖评审委员会发出呼吁：请把诺贝尔和平奖授予刘晓波。德国之声第一时间电话采访了徐友渔教授，谈到这封信的初衷及刘晓波如果获得诺贝尔奖后于中国的意义。徐友渔表示：<br />
<br />
&quot;我其实认识刘晓波时间非常短，我不是他的老朋友，我之所以发出这个呼吁，是赞成他的主张，并不是因为他是我的老朋友或好朋友，想增加他的荣誉。我觉得，他如果获得诺贝尔和平奖是实至名归的，他的理念我是非常赞同的，他应该获得，不但是我这样认为，包括哈维尔、达赖喇嘛都会赞同他获得诺贝尔和平奖，这么做我觉得能体现的理念是用和平的方式推动中国的民主和自由，中国存在着潜在的这种可能性，比方说，中国使用暴力太厉害了，由于社会不公正的情况也太厉害了，公众产生了报复的心理， 我觉得他体现的是那种理性的、冷静的、和平的方式来改变这种社会状况，这对中国的进步是非常有利的，可以避免社会的动乱和更大的暴力、冤冤相报的情况出现，我觉得他和诺贝尔和平奖的以前获得者的思想理念是完全一致的，他如果能够获奖对中国和平的方式、理性的方式推动社会转型有莫大的好处，所以我非常希望他能够获得这个奖&quot;。<br />
<br />
