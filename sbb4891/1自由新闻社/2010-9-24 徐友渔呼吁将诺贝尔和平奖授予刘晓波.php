<br />
（以下为徐友渔呼吁信全文）<br />
<br />
请把诺贝尔和平奖授予刘晓波<br />
<br />
我，徐友渔（Xu Youyu），哲学家，中国社会科学院退休政治哲学教授，2001-2002年度奥洛夫帕尔梅客座教授（the Holder of the Olof Palme Chair, 2001/2002）谨向诺贝尔和平奖评审委员会发出呼吁与请求：请把诺贝尔和平奖授予中国《零八宪章》的发起人刘晓波；谨向欧洲各国媒体和公众发出呼吁与请求：请发挥你们的影响，发出你们的声音，促成诺贝尔和平奖授予刘晓波。<br />
<br />
刘晓波有许多朋友和支持者，但我很晚才认识他，第一次见面也不过是在几年前。当刘晓波在上世纪八十年代中期在中国文学界成为著名人物时，我在英国牛津大学从事访问研究（as a visiting research fellow），我通过海外中文刊物了解他的观点。我当时就认为，与许多人的印象相反，他并不仅仅是靠激烈的语言和尖锐的批评引人注目，他思想彻底，在批判中国主流意识形态和教条方面，他比其他有影响的知识分子走得更远。<br />
<br />
在1989年中国学生发起的民主运动中，我有机会进一步观察刘晓波。他一直在国外访问，但当镇压的迹象已经出现，个别人着手安排逃避到海外去的时候，刘晓波反其道而行之，中断了访问，返回北京，投身到这场争取民主的运动中。6月3日到4日的整个晚上我都在天安门广场，就在纪念碑附近，当时刘晓波和其他三位知识分子参加到学生的绝食中，是他们在4日凌晨劝说和引导学生和平撤离广场，并与镇压的军队谈判、协商，使得撤离顺利完成。我深知，刘晓波和他的战友作出了困难的、痛苦的，对于一直受激进思想教育的中国人来说极难作出的决定，这个决定挽救了成百上千学生的生命。<br />
<br />
刘晓波在1989年民主运动中的表现说明，他已经从一个著名的文艺批评家转变为关注社会和政治问题的公共知识分子和人权活动家，他在1989年的行动体现的特征也贯穿于往后他所有的言论和行动中。这些特征是：一、大无畏的勇气，面对危险和镇压不是躲避，而是迎面而上；二、追求和捍卫人权、人道、和平等普世价值；三、坚持理性、非暴力、对话和协商的方法。<br />
<br />
多年以来，刘晓波是中国大陆维护人权和争取民主活动的主要发言人和组织者，他带头为因为言论表达而入狱的作家、知识分子发出抗议，为失去土地、房屋的农民和城市居民呼吁，为在西藏、新疆保障少数民族的宗教文化权利，实现少数民族和汉族居民的和平友好相处提出建议。在维护中国公民基本权利的一系列抗议活动中，刘晓波特别强调，中国公民的自由和权利是得到现行中国宪法、法律，以及中国政府签署的一系列联合国保障人权和公民权利的宣言、一系列国际公约的保障的，特别强调中国政府本身有义务和责任遵守本国宪法、法律和国际公约，应当兑现它对于中国人民和国际社会的承诺。<br />
<br />
刘晓波在2008年发起签署《零八宪章》，其本意和出发点就是要再次重申，中国政府已经承认的《世界人权宣言》和已经签署的《公民权利和政治权利国际公约》对于中国人民和中国政府而言，都是行动的准则，中国应该认同这些宣言和公约体现的普世价值，中国应该是国际大家庭中合格的、负责任的成员。刘晓波为此被捕入狱，这是他第三次为了中国的自由民主而被抓捕。2009年圣诞节，他被判处11年徒刑。刘晓波在法庭的最后陈述中说，他没有敌人，也没有仇恨；他对所有监控过他，抓捕过他、审讯过他的警察，起诉过他的检察官以及判决过他的法官说，他们都不是他的敌人，虽然他不接受这些人对他的监控、逮捕、起诉和判决。<br />
<br />
作为政治理论家、中国关注和捍卫人权的公共知识分子以及《零八宪章》的签署者，我要强调指出，中国法院在对刘晓波的刑事判决书中公然把刘晓波参与起草《零八宪章》、征集签名以及该宪章的内容作为判决刘晓波有罪的证据，这是对人类普遍价值，对国际大家庭的共同准则，对中国人民的赤裸裸的挑衅。<br />
<br />
我认为，诺贝尔和平奖体现和象征了文明世界的核心价值：尊重生命和人的尊严，尊重信仰和表达的权利，既然刘晓波和《零八宪章》的众多签署者仅仅是因为重申这些价值而受到迫害和压制，那么文明世界需要对这种公然的挑衅作出回应，把诺贝尔和平奖授予刘晓波是一种最有力的回应，是对人类珍视的价值的明白无误的再次重申，是对13亿中国人民争取民主自由的巨大声援，也是保卫世界和平的重大举措。中国当局可以在国内破坏宪法、践踏法律、为所欲为，需要外部的声音、国际社会的声音提醒中国当局。授予刘晓波诺贝尔和平奖是非正面对抗的，同时也是权威性的、有效的提醒。<br />
<br />
我认为，刘晓波的思想、行为与达赖喇嘛尊者、图图大主教、昂山素姬等人的思想、精神和行为路线完全一致，他们都是力图通过理性与非暴力手段，通过渐进的、说服与协商的方式维护人权，促进社会和平地实现转型。在目前中国各民族、各地区、各阶层的抗议运动中，我们有必要警惕、预防暴力倾向的出现，授予刘晓波诺贝尔和平奖会起到这样的作用：全中国、全世界为争取人权而斗争的人们将在理性和非暴力的抗争中看到希望和吸取力量，全世界、全人类将在埋葬专制统治的同时埋葬暴力。<br />
<br />
徐友渔<br />
<br />
