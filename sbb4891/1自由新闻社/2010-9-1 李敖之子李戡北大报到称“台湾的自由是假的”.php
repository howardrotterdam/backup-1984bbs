<img src="http://ss16.sinaimg.cn/orignal/5b56ce77t74b7de167f6f&amp;690.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
李敖之子李戡北大报到 最想听林毅夫课<br />
<br />
中新社北京9月1日电 题：李敖之子李戡北大报到 最想听林毅夫课<br />
<br />
中新社记者 马海燕<br />
<br />
李敖之子李戡今天来到北京大学经济学院报到，成为今年13名赴北大就读的台湾高中生之一。 大批媒体如影随形，让李戡成为当天北大报到的2270多名本科新生中最耀眼的一个。<br />
<br />
上午十时许，戴着眼镜、清瘦文静的李戡现身北大五四路经济学院报到点。在缴纳入学通知书核对信息领取手机号后，他到马路对面的国际合作部迎新点签到，所有港澳台学生均需在这里登记。随后在交费处交纳包含学费和住宿费在内的6388元人民币现金，再到北大百年讲堂领取饭卡、住宿证等相关证件，入住 34号楼四人间的学生宿舍。一路上，李戡都谦和有礼，有问必答，18岁少年已显露出乃父机智应对的风范。<br />
<br />
李戡和母亲王小屯是在参观上海、杭州之后于昨晚抵京的，父亲李敖没有陪儿子同来这所5年前曾发表演讲的著名高等学府。当被问及对于没有父亲仍有这么多媒体等候有何感受时，李戡笑言：“媒体辛苦！”<br />
<br />
李戡在台湾的学测成绩约是前3%，被台湾大学和北京大学同时录取。他在给北大的入学申请书中说，“我的父亲李敖困于台湾六十年，以未念北大为憾，我愿意能超越这六十年的海峡，衔接这一心愿。”李戡的祖父李鼎一、大姑李民、二姑李讯、大姑丈周克敏均毕业于北大。<br />
<br />
李戡说，北大最吸引他的是文化氛围、学术环境。“北京很大气，台湾则小气得多”，“台湾的自由是假的，只有‘台独’的自由是真的。”几次访问大陆都只到过发达城市的李戡，希望在祖国大陆多走走，特别是到西北内陆省份去看看。<br />
<br />
因为对台湾教科书和教育体制的不满，李戡写了一本《李戡戡乱记》。当被问及会不会写“戡乱大陆教科书”的书时，李戡表示：“你们没看过台湾的教科书，不知道荒谬到了没法接受的程度，歌颂军国主义、歌颂日本人。大陆的教科书没有这些吧？至少在大的方向上没有问题。”<br />
<br />
饱览群书的李戡选择经济学院就读，而没有选择其家学渊源颇深的人文、历史学科，李戡说，那些只是兴趣，而他要选择一门需要刻苦钻研的学科。到经济学院学习，他最仰慕的是林毅夫，希望有机会听到他的课。李戡表示他也会去选政治课等有着大陆思想教育特色的课程。他对看横版书、简体字也没有任何障碍。<br />
<br />
高中时并不参加任何社团、与同学交往也不多的李戡说，在北大会参加一些学生社团，也会在学生宿舍住宿，在学校食堂吃饭。李戡此次还带着父亲的新书《阳萎美国》来，表示会送给同学。<br />
<br />
李戡表示，到大陆来念书是我自己选的，以后还想在这边发展。至于会不会在北大读研究生，“以后再说”。<br />
<br />
李戡的母亲王小屯则一路都离儿子远远的，显得格外低调，“今天的主角是他，不要照我。”当被问到会不会阻止儿子在学校交女朋友时，王女士露出一个母亲慈爱的笑容，“这是自然的事，我不会过问，缘分来了挡也挡不住。”<br />
<br />
至于人未到就先起和大陆人气作家韩寒的“口水战”，李戡说之前的事情都是媒体炒作出来的，他对韩寒并没有个人成见。李戡母子都希望新学期开学后可以安静下来，不被打扰，认真读书。<br />
<br />
