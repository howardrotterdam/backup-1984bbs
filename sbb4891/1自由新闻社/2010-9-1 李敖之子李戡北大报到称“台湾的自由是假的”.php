<br />
　　“我希望他能在这里学习北大人的努力和刻苦精神。”陪同儿子前来报到的李勘母亲王小屯在接受记者采访时补充说，台湾大学的学生求学精神不如北大，况且李勘的兴趣也在经济专业。同时，她还希望媒体今后不要来找他，让他在北大过单纯、平静的学生生活。<br />
