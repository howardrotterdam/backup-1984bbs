&nbsp; &nbsp;本报南京6月12日电 特约通讯员罗正然、特约记者邱柏星报道：6月6日晚，夜黑如墨，南京军区某团夜训场上，轻武器射击训练正在进行。记者发现，参训者轻武器准星上的荧光粉已全部被清洗干净，射击夜训场不再有星星点点的荧光闪烁。 　　某新型轻武器列装部队后，夜间射击训练难度加大。素有“夜老虎连”之称的七连，率先成立夜间射击攻关小组，围绕黑暗条件下的射击要求进行研究。他们很快推出这样一个“小发明”：到商店买来荧光粉，用胶水搅拌后涂抹在轻武器的准星上。这样，夜间准星便会自动发光，辅助搜索瞄准目标。这个“小发明” 第一次用于实弹射击考核，全连射击成绩明显提高。<br />
　　这项“小发明”在该团引起不小的反响，一些单位相继效仿。正当团机关有关部门准备对这一方法推广时，团领导经过调查却提出异议：借助荧光粉虽能提高射击精度，但战时会不会暴露目标？他们组织多次试验，结果证实了这一担心。夜训中，团领导安排部分官兵远距离观察射击阵地，结果让官兵大吃一惊：枪上抹荧光粉的目标，清楚地暴露在视线之中。<br />
　　这项“小发明”虽能提高训练成绩，但战时却容易暴露目标！荧光粉事件给官兵上了深刻一课：搞发明创造，不仅要看能否提高训练成绩，更要符合实战要求。团里当即决定，把轻武器准星上的荧光粉清洗掉。同时，他们用实战这把尺子，对基层20多项小发明进行衡量验收，取消了用手电筒放大靶心亮点、机枪分解结合增大卡槽等4种与实战要求不符的做法。团里还规定，凡经不起实战检验的小发明、小革新，一律不予立项。<br />
