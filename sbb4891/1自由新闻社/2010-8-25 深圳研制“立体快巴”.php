													<h2>深圳研制“立体快巴”</h2>
						
						
																											<div id="postmessage_797472" class="t_msgfont">立体快巴明年底将空中载客&nbsp; &nbsp;美国网友:被中国打败<br />
<br />
2010年08月25日17:59<br />
来源：中国广播网<br />
<br />
<img src="http://img.hexun.com/2010-08-25/124708371.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
上层载客下层通车的“立体快巴”（来源:南都网）<br />
<br />
　　中广网广州8月25日消息 据中国之声《新闻晚高峰》17时ZZ分报道,科幻大片中汽车在空中“飞行”的场景将变成现实,“立体快巴”通过安全评审,明年底有望载客。<br />
<br />
　　科幻大片中汽车在空中“飞行”的场景不久我们将在实际生活中看到,上层载客下层通车的“立体快巴”项目目前已经进入制造环节。这项发明在今年的北京科博会上首度公开亮相后一炮走红,还登上了《纽约时报》的封面。美国网友惊呼:“美国创新能力走下坡路、已被中国'打败’”。目前,快巴刚通过专业机构的安全性评审。明年年底,立体快巴将在门头沟载客运营。详细情况我们连线今天的新闻编辑田娜,请她介绍一下。<br />
<br />
　　主持人:立体快巴长什么样？能否介绍一下？<br />
<br />
　　记者:立体快巴它犹如装了“高跷”的公交车,上面可载千余人,下面镂空,小汽车在其“肚子”下可自由穿行。据发明者――深圳华世未来泊车设备有限公司负责人宋有洲介绍:每辆“立体快巴”分为4节,每节长10米。车体底层高度为2.2米,总高度4.4-4.5米,车身“增肥”至两条车道宽,这样车体下面空间就能并行两辆小汽车。“立体快巴”行驶速度每小时约60至80公里,较一般公交快。它可与设在路边的公交站或天桥对接,无论是直行、停站,都不会影响底下汽车的通过。<br />
<br />
　　主持人:立体快巴有什么特点？<br />
<br />
　　记者:发展“立体快巴”无需占用新的道路资源。巴士和快速公交系统(BRT)占用路面资源多,而地铁投资成本高、建设时间长,“立体快巴”刚好能避免以上缺点,并能令塞车情况减少20%至30%。这种零排放的“立体快巴”是我国完全自主创新、全世界独有的产品。<br />
<br />
　　快巴每公里造价约5000万元人民币,较地铁每公里造价5亿至8亿低了九成,一年可建40公里,而建同样里程的地铁则需3年。为了让充电方便,沿途还设置了充电桩,车辆行驶时只要接触就能充上电(600627,股吧)。此外,夜间不使用时,“立体快巴”可停泊在车站,不需另占停车场,也不影响下方车辆继续通过。立体快巴”最大优点是,在不改变平面交通形态的条件下,使交通立体化,使载客量可达到地铁的载客量,降低城市公共交通投入,并提高运行效率。<br />
<br />
　　主持人:我想可能听众朋友会有很多疑问,比如这种立体快巴是否安全,如何转弯？<br />
<br />
　　记者:的确,我在一听到立体快巴的时候也会产生这种疑问。关于这些问题,发明者也有一些解释,对于如何确保行驶安全的问题。据介绍,快巴行驶线路是半封闭的,小汽车通行需要有专门的入口和出口,该出入口设在快巴站点,机动车可在站点驶入快巴肚内,即快巴下边的车道。此外,快巴在站点之间是封闭的,从而避免汽车随意驶入车道发生事故。另外,快巴还设置了雷达扫描系统,如果小车在行驶过程中跑偏,离快巴的“腿”太近,灯就会自动闪亮避免交通事故发生。即使小车撞击到快巴,也仅是撞到它的“腿”,这些“腿”是由很重的钢材构成,很能抗撞。乘客都在2.2米以上高度,可以确保完全。<br />
<br />
　　关于快巴如何实现拐弯？在设计中,快巴适用于在主要交通干道通行,完全参照快速公交优先的法规。即立体快巴在与小汽车同步运行时,遇到转弯、路口时,前方红灯及快巴车身上的红灯会亮,小汽车先停十几秒,让快巴先转过或者通过后再走。转弯时快巴也会把速度减至每小时10到15公里,从而确保转弯安全。<br />
<br />
　　主持人:万一出现意外,乘客如何逃生？<br />
<br />
　　记者:快巴的逃生系统参照了飞机上充气滑梯的原理,发生事故时,两侧车体可自动下翻至地面,下翻后车内栏杆、扶手即转化为滑梯护栏,从而让乘客有序逃生、避免发生踩踏。发生事故后,快巴车头、车尾两侧的安全栏杆会自动拦截后续车辆进入车道。根据目前进度,首辆车开始设计制造后,三个月就可以制造出样车。今年年底,门头沟区的轨道有望开建,明年7月份,首辆车将上线试运行。<br />
<br />
