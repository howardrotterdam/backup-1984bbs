													<h2>新华网：百无一是谈民主</h2>
						
						
																											<div id="postmessage_221768" class="t_msgfont">窦含章<br />
<br />
&nbsp; &nbsp; 许多人说：“民主是个好东西”。可这句话要想成为真理，恐怕要在前面加上很长一串限定语。在世界各地，刚刚发生和正在发生的许多事情就展示了民主不好的一面。<br />
<br />
&nbsp; &nbsp; 在阿富汗，大选已经结束，选举结果仍未公布，但两位主要候选人卡尔扎伊和阿卜杜拉看起来都不想接受失败的结果，人们普遍担心，选举纷争可能引发两人各自代表的普什图族与塔吉克族之间的民族仇恨。在伊朗，6月的大选引发了严重骚乱和抗议，虽然事态已渐渐平息，但由大选造成的社会裂痕，恐怕需要很长时间去平复。<br />
<br />
&nbsp; &nbsp; 在台湾，台湾地区领导人马英九时下的处境只能用“百无一是”来形容。民进党和许多媒体把“莫拉克”台风造成的惨重伤亡都怪在他头上，把他骂了个狗血喷头，其民意支持率最低时只有16%，一份影响很大的民调竟然称“82%的台湾民众认为马英九应该下台”。灾情严重，政府救灾有疏失，挨些骂也正常，但支持率如此之低，就有些离谱了。既然马英九如此不堪，一年多前，台湾人民又为什么要高票选他上台呢？这种民意认识上的极端摇摆，是不是有非理性的成分呢？<br />
<br />
&nbsp; &nbsp; 再看大陆。这几年随着互联网技术的发展，社会的进步，中国人的言论空间进一步扩展，互联网成了老百姓监督政府，发表意见的重要平台，在推动政府依法行政、科学决策、民主决策等方面发挥了重要作用。但是，互联网的这种自由，特别是它的匿名性，也带来了许多问题，一些人把现实生活中的个人怨气带到网上，什么都看不惯，什么都要骂，特别在对待政府时，不管政府怎么做，与以前比是不是进步，都一概否定，完全把言论自由变成了一种泄愤渠道。比如，盖学校，建不好人们肯定要批评政府不重视教育，盖好了也不行，三峡有一所希望小学修建花了350万，一些人就指责花钱太多，是政府的“形象工程”。再比如，经适房，老百姓都对高房价不满意，政府为了解决百姓住房难，计划未来三年在全国建390万套经适房，可这也不对，有些人批评经适房“既不公平又低效率”，是“权贵的盛宴”，在媒体和网上彻底废除经适房的言论一浪高过一浪，可就是没人愿意去问问通过经适房建设改善了居住条件的中低收入人群的感受。<br />
<br />
&nbsp; &nbsp; 在这些事例中，所谓民主展现出来一种缺乏宽容的暴戾之气，身处其中的人对自己不接受的观点、人、事、物、组织，不管对方身上有没有可取之处，总是想全面否定，非把对方骂得“百无一是”不可。而这种“百无一是”，带来的往往是误解、仇恨和纷争，是矛盾的日益尖锐和不可调和，发展到极致，就是社会动荡、百姓生活水平下降。<br />
<br />
&nbsp; &nbsp; 这种与民主共生的“百无一是”，依靠民主自身是很难克服的。比如在选举政治中，竞争的双方利益经常完全相对，否定对方就成了一种必须，如果缺乏历史传统、调和机制和能够控制局势的社会中坚力量，双方就会无所不用其极地攻击对手，整个社会就可能被撕裂，陷入永无止境的纷争，经过 2005年选举而“分治”的巴勒斯坦和台湾地区的两党恶性互斗就是很典型的例子。另外，在利益多元化的社会，政府无论采取何种政策，都会出现受益不均的问题，谁受益少或利益受损，谁就会攻击政府。在这种情况下，如果没有明智强大稳定的社会中坚力量控制局面、调整利益分配，政府就会变得无所适从，就会失去方向和目标，政策就会因缺乏长远规划而失去连贯性，变得朝令夕改，整个社会也会因此陷入无尽的纷争之中。<br />
<br />
&nbsp; &nbsp; 那么，是不是说民主就不好呢？也不是。民主作为一个广义的概念，普遍存在于世界各国的政治生活之中，符合国情的适度民主可以扩大百姓的自由和权利，可以加强对政府的监督，好处不少。可是，有关民主好不好的争论，在多数时候都会演变为毫无意义的争吵。因为争论者对“什么是民主”这一基本问题往往无法达成共识，因此争论只能是自说自话，得不出什么有意义的结论。要为“民主好不好”这种形而上的争论找到出口，还是要给它一个现实的落脚点，我认为，评判的根本标准还是看老百姓到底要什么。<br />
<br />
&nbsp; &nbsp; 我经常在想，老百姓最想要的究竟是民主还是安定富足的生活？想来想去，答案只能是后者。因为不管什么形式的民主，不管它是一种意识形态，还是一种制度安排，都不过是一种手段，其最终目标是为了让世界和平，让老百姓过上好日子。因此，不管是哪种形式的民主，对其评判的最终标准只能是老百姓的生活水平和世界稳定。让老百姓富足幸福，让世界和平安宁，这样的民主就是好的；让老百姓食不果腹，让世界战乱动荡，这样的民主就是坏的。<br />
<br />
&nbsp; &nbsp; 因此，对于民主切不可绝对化，既不要说“民主是个好东西”，也不要说“民主是个坏东西”。关键还是要看看老百姓锅里的油水多不多，这样的标准实在、可信，比空谈民主争得一地鸡毛强得多。(窦含章) <br />
