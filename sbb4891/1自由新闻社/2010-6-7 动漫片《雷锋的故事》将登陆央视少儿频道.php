再说学雷锋<br />
作者：谢不谦 提交日期：2007-3-7 23:01:00 正常 | 分类: | 访问量：1649<br />
<br />
　　雷锋是个符号，毛时代的符号，富有特定的精神内涵。很多国人，却把他当做了通用符号，好像雷锋就是好人，好人就是雷锋，说美国政府不号召学雷锋，却人人都是雷锋，好像雷锋是基督徒似的。<br />
　　雷锋是很有些国际影响。记得二十多年前，我在狮山读研究生，来了一批美国戈申学院的游学生，戈申学院就是美国的达州师专，狮山却奉若神明，住的是“熊猫馆”，吃的是“熊猫餐厅”。某日，游学生某男，恐龙似的，窜来筒子楼。大家不顾外事纪律，热情相邀，请茶，聊天。我略通鸟言兽语，代为翻译。他说，他从小就向往中国。大家问：“为何？”那娃竟说：“中国有个雷锋！”逗得大家哈哈笑，笑得他绿眉绿眼。翌日英语课，来个戈申洋妞，老师介绍说，是我们的口语老师。洋妞一笑，说她原是哈佛学生，为来中国一游，临时客串戈申学生。周末，征得外办同意，哈佛洋妞带几个金发碧眼高鼻子，来筒子楼交流。我们就说那个男恐龙如何羡慕中国有个雷锋，全体洋妞耸耸肩：Stupid！傻冒瓜娃子之谓也。无独有偶。我访学哈佛，见科学楼大厅，周末都有一叠免费报纸，读者自取。名 MIM，全名：Maoist International Movement，毛主义国际运动。凶险哦！毛主义，不仅中国移动，还国际运动全球移动。是美国濒临绝迹的左翼人士办的家无名小报，声称：一，美国工人阶级已经贵族化，不再是共产主义革命主力军；二，中国自毛去世后，资本主义全面复辟。我耳熟能详但已久违的“文革”腔。报上一文，作者是“文革”中曾来华访问的美国青年，盛赞知识青年上山下乡，盛赞中国人人学雷锋。他至死可能都不知道，他耳闻目睹的，是官方导演的行为艺术，而非中国庐山真面目。我这才明白，美国也有傻冒瓜娃子，被毛时代的假象所蒙蔽，至今执迷不悟。我当时就想，上帝为何不给美国人送去个毛主席哦？我在哈佛广场地铁站候车时，旁边有一白人中年男子，见我这个老外，很礼貌地一笑。我就把报纸文章给他看，问他感想。他草草浏览一过，也耸耸肩，说：Stupid！相视一笑。<br />
　　现在某些国人跟着老外傻冒，誓死捍卫雷锋，把助人为乐、尊老爱幼、献血捐款等善举，都跟学雷锋扯到一起。若有人解构雷锋、不赞成学雷锋，就是人不为己天诛地灭之流似的。我只能说：国人太健忘。须知，当年那个真雷锋做好人好事，是有个大前提的，那就是阶级立场：对同志春天般温暖，对敌人严冬一样冷酷无情。敌我分明爱憎分明，这才是真雷锋在“阶级斗争为纲”的革命年代，得以被塑造为全民英雄，而伟大领袖号召学习他的原因。否则他就只是个普通好心人而已。普通好心人，不讲阶级斗争，反而是罪名。列宁说：“对敌人的仁慈，就是对人民的残忍。”记得雷锋日记也抄录了这句格言。<br />
　　记得上小学时，少先队组织学雷锋做好人好事，都是去帮助烈属军属或贫民孤老奶奶孤老爷爷，扫地、挑水等。班主任反复叮嘱我们，助人为乐，先要弄清对方的阶级身份。但阶级敌人脑袋上没有刻字，我们须提高警惕。记得那时，宣汉地富反坏右“黑五类”，多被罚去干体力活拉板车，上坡下坡，天天遇到。人人右臂戴一白布黑字袖章，以让革命群众识别。某日雨后，遇一干瘦老头，吭哧吭哧，拉一板车煤炭上坡，车竟慢慢往下滑，情况危急。耳边也没响起毛主席教导，眼前也没闪现雷锋形象（这是当年作文套语），完全是出于本能反应，我急跑过去，从后边使劲帮他推住车，上得坡来，老头连声谢谢，我心里也有一种快乐感。晃眼一瞥，看见他右臂上白色袖章，分明是个“右”字！吓得扭头就跑，心里说不出什么滋味，象是偷偷摸摸干了坏事似的。想起班主任谆谆教诲，从此不敢轻易帮助陌生人，哪怕是举手之劳，我都来个熟视无睹，生怕又遇到阶级敌人。学雷锋，真是学得我是非分明立场坚定，而人皆有之的同情心恻隐心逐渐丧失。那个真雷锋，激发了我们这一代人的阶级感情，却扼杀了我们这一代人“老吾老以及人之老，幼吾幼以及人之幼”的善根。<br />
　　当年歌唱雷锋精神的革命歌曲铺天盖地，什么“爱憎分明不忘本”，什么“对敌人毫不留情”，云云。须知毛的敌人就是雷锋的敌人，倘若雷锋遇“黑五类”遭遇困难或不幸，他会大发慈悲伸出援助之手？而当毛宣布刘少奇邓小平等老一代革命家是敌人时，他的立场又会站在哪一边？真雷锋幸而早死，若遇“文革”，以他对毛的忠诚，读毛主席的书，听毛主席的话，照毛主席的指示办事，绝对是“文革”急先锋。请读贺敬之著名的台阁体长诗《雷锋之歌》，那才是当年毛版的真雷锋精神。所以，我曾说，红卫兵就是雷锋的“文革”版。而现在，抽去这些最实质的精神内涵，而把古今中外都有的美德善行堆积在一个空壳上，借壳上市，说这是雷锋精神，号召大家学习，真是活见鬼！最令我不解的是，今日后生，竟然还有以什么雷锋，来怀疑我的人格。我不是英雄更不是圣贤，浑身缺点，但我可以毫无自愧地说：我比雷锋好比雷锋真实，比他更有同情心更有爱心。因我，信奉人道主义，凡我力所能及的，无论高低贵贱什么阶级，我都愿去帮助。而不象他，先有个阶级立场，最后还要记在日记本上。我做好事，不是响应号召，也从不记流水账。那是我个人的心情我个人的快乐而已。<br />
　　最后，声明一下，自从上大学后，痛改往日之非，公车上绝对给老弱病残让座，而且要求我的学生给老弱病残让座。我曾在江安校区给学生上文化素质课时，盛赞川大学生上车排队，主动给老师让座的风气，也呼吁他们把这个风气带到社会上去。这不是学雷锋，因为凡是老弱病残，遇到困难，都应该得到年轻人的关爱与帮助，而不问他们的阶级出身。<br />
　　雷锋是一个可悲可怜的历史人物，把他还给历史吧。愿雷锋同志的英灵安息。我是绝不会来学你的。我的学生若学你，他或她就不是我的学生。</div>

							
							
							
							
