<br />
30集动漫片《雷锋的故事》8月7日晚将登陆中央电视视少儿频道。<br />
<br />
　　由沈阳春秋动漫文化有限公司精心打造的30集动漫片《雷锋的故事》，8月7日晚将登陆中央电视视少儿频道，在少儿星空栏目首播。署假期间，动漫片《雷锋的故事》还将在中央电视台的一、四、七、少儿频道安排重播，之后还将陆续在全国省市级电视台播出，全国少年儿童都可一睹崭新的“动漫版”雷锋形象。<br />
<br />
　　雷锋的故事有着鲜明的时代内涵和现实意义。雷锋，是辽宁这片土地上成长的伟大的共产主义战士。雷锋忠于共产主义事业，毫不利己专门利人，在各种不同的工作岗位上干一行爱一行，把有限的生命投入到无限的为人民服务之中去，在平凡的工作中为社会主义、共产主义的事业而奉献自己的力量。雷锋在实践中表现出来的全心全意为人民服务的共产主义精神，被誉为雷锋精神。对当前精神文明建设、社会和谐发展有着积极的现实意义.<br />
<br />
　　用动漫形式表现爱国主义题材,《雷锋的故事》为“主旋律”影视剧创作作出了有益的探索和尝试。据2008年国家人口最新统计数据，我国少年儿童总数约占全国总人口的30%，即4亿人左右。<br />
<br />
据中国动画播映机构统计数据显示，2006年以来，经中国广播电视总局批准，全国已有33个少儿频道开播，上星频道每天各播出国产动画片超过600分钟。中央电视台少儿频道每天播出的国产动画片达360分钟，并已在全国31个省、296个省辖市落地。少儿频道的动漫片已经成为电视播出节目中不可忽视的播映内容。动漫片作为一种新兴的影视传播模式，受到少年儿童的喜爱，并在潜移默化中影响着少年儿童的身心健康成长。<br />
<br />
　　然而，当今影视视作品中，适合少年儿童观看的动漫作品少之又少。国外的一些动漫片占领着国内动漫市场的大部分份额，其中不少不健康、不适合中国国情的作品，影响着少年儿童健康成长。一些外国动漫漫作品肆意染指、篡改中国传统文学名著，矮化中国传统文化。同时，动漫产业作为一个新兴的高科技产业，辽宁更是远远的落后于南方，缺少有影响力的动漫作品。如何创作更多更好健康向上、喜闻乐见的作品，成为辽宁动漫人的责任。<br />
<br />
　　动漫版《雷锋的故事》共30集，分成四部分，概括和展示了雷锋从一个苦孩子到共产主义战士的成长全过程。<br />
<br />
　　《雷锋的故事》故事情节通俗易懂画面鲜明生动，富的感染力，是适合少年儿童观看的充满艺术魅力的好作品。<br />
<br />
　　动漫版《雷锋的故事》柔和中外动漫优秀作品的各种要素，用少年儿童最喜欢、最容易接受的表现形式，寓教于作品中，<strong>达到教育从小抓起、潜移默化的作用。</strong>电视片综合利用二维和三维动漫制作技术，达到动画片多层次、多空间的立体效果，剧情和人物刻画方面生动活泼，儿化语言受到小朋友们的欢迎。观看样片的中小学生笑地嘎嘎的，许多家长当场提出要买片收藏。<br />
<br />
　　沈阳春秋动漫文化有限公司是沈阳市浑南动漫产业园区的精英企业，公司以弘扬民族文化，打造具有中国特色的影视动漫画为己任，努力创作民族的、健康的、科学的、和谐的、充满阳光的、有情趣的动漫影视作品，丰富中国的动漫产业。<br />
