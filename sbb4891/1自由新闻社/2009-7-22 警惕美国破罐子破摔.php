													<h2>警惕美国破罐子破摔</h2>
						
						
																											<div id="postmessage_174066" class="t_msgfont">无论是在六十年前，还是在三十年前，美国的灭亡中华之心都没有现在这么强烈过。　　<br />
<br />
为什么我敢作出这样的断言？我这是有逻辑基础的。　<br />
<br />
世界上的万事都有因必有果，在历史上还没有发生过，种下大豆，却长出西瓜的事例；同样，也没有出现一头老鼠生下了猫。　　<br />
<br />
猫，生下的必然是猫，老鼠，生下的必然是老鼠。如果有人告诉你，他家的老鼠居然生了只猫，那个人如果不是疯子的话，就是骗子。　　<br />
<br />
那么，中国究竟干了什么，让美国人日夜寻思要灭亡中国呢？　　<br />
<br />
原因很多，但目前看来，最重要的一条原因就是：美国欠了中国天文数字的巨额债务，这个债务是如此之重，对美国来说，是难以负荷的。中国外汇贮备2万多亿，大部分是美元资产，主体是美国国债，超过7000多亿。这两万多亿的外汇贮备，可理解为是美国人向中国人借钱打出的白条。如果一下子要还得话，美国这个国家都要破产了。显然，美国人是不会还得，因为实在是还不出了。因为美国不单单只欠中国，它还欠着全世界。　　<br />
<br />
2008年10月13日　中央台就报道由于美国国家债务日益增加，位于纽约时代广场上的国债钟位的数字近日已经不够用了，这叫“国债钟撑破事件”。至此美国国债超过10.2万亿（注意这还是2008年的数字，随着奥巴马出台刺激经济计划，美国国债正急剧上升）加上美国政府其他欠债四五万亿，平摊下来，美国家庭平均负债10多万美元。　　<br />
<br />
美国人即使把整个美国卖了，也还不上这笔钱。所以，对美国来说，他唯一的选择就是：老子不还钱了！　　<br />
<br />
美国不还钱，本就是它的战略。美国的战略就是消耗全世界的物资，以供己用。如果你让美国把这笔钱全还上，岂非让美国人白花了这么多年的心思？美国自20世纪以来一直在打战，主要战役有一战，二战，越南战争，朝鲜战争，科索沃战争，两次海湾战争，阿富汗战争，冷战以及反恐战。美国人打了这么多年的战争，抛下尸首上百万具，当然，被它杀死的恐怕要以千万亿万计了。美国人这样不计血本的干，其目的无非就是想占有全世界的资源以供自己消耗，如果到头来这笔帐美国人照样要还上，那美国人岂非要气炸？　　<br />
<br />
所以，不还账，免费吃喝本就是美国的战略。　　<br />
<br />
如果读者认同我这个逻辑，那么，接下来的问题就是：美国怎样才能做到不还钱？　　<br />
<br />
我们先来看看在中国成为美国最大的债主之前，哪个国家是美国的原最大债主？　　<br />
<br />
是日本！　　<br />
<br />
1993年，中国外汇储备仅210亿美元，到2008年底、2009年初，外汇储备高达19500亿美元。16年增长近93倍。而在2008年以前，日本始终是美国的最大债权国。日本至今拥有美国国债6000多亿，官方外汇贮备1万多亿，日本民间还有外汇贮备3万多亿。总体而言，美国欠日本的最多，中国其次，两者相加，占了美国外债的50%左右。　　<br />
<br />
那么，美国人欠了日本人这么多的钱，美国人会还的吗？当然不用还了，因为日本人本就被美国人以隐蔽的方式占领着，不能算是一个主权独立的国家。美国人欠着全世界这么多钱，它可从不曾考虑过要还钱，唯一让它烦恼的就是中国了，怎样才让中国也像日本一样变成附庸国，从此赖掉几万亿的欠债？　　<br />
<br />
我考虑了一下，美国要赖掉这天文数字的欠债，可以有三种方式：　　<br />
<br />
一是把中国变成日本一样的附庸国，失去国防权与外交权。令中国再也不敢向美国要回这笔钱。　　<br />
<br />
二是分裂中国，把中国肢解――既然中国都不在了，钱当然也就不用还了。即使有后来的政府继承了中国的名字，陷于内战的中国也没心思更没实力敢于要回美国人的这笔巨额欠债。　　<br />
<br />
三是与中国开战，一旦美国人找个什么借口与中国打战的话，作为交战国，自然这笔钱也不用还了。　　<br />
<br />
以上三种方法，恐怕对美国人来说，分裂中国是最容易实施的选择。因为要同中国直接开战的话，美国只怕自身也要灰飞烟灭了！如果要把中国变成附庸国，这个方法对美国人来说，难度也很大，中国凭什么乖乖地要成为美国的附庸国， 就凭那批吃里爬外的汉奸就够了吗？恐怕不够。所以，对美国人来说，发动台独，疆独，藏独是最好的选择了，即使投入几千亿美元发动针对中国的分裂运动，与要归还中国几万亿美元比较，美国人还是赚大了。　　<br />
<br />
所以，对中国人来说，当前这个世界的最大敌人就是美国了。这是由当前的利益格局注定的。中国已经钻进了美国的圈套，只要期待英明的中国领导层，带领中国人走出这一圈套。　　<br />
<br />
天佑中华！　　<br />
<br />
附录：　　<br />
<br />
关于外汇贮备的一个经典比喻：　　<br />
<br />
中国固然是美国的第一大“债主”，美国同样是“中国制造”的第一大买主。就象吃饭不给钱，中国就是饭馆店主，美国就是签单的顾客，店主一分钱都没收到，还在炫耀顾客欠他多少钱，这都二到什么程度了？　　<br />
<br />
二、关于我为何要写这篇文章　　<br />
<br />
我们七十年代出生的人总有个观念，总以为这个国家是我们的。因此，无论如何，要为我们的国家出谋划策，为国分忧是我们的使命。&nbsp;&nbsp;　　<br />
<br />
一四川地震志愿者所言，为本人认同。　　<br />
<br />
三、航母太多了的美国与没航母的中国　　<br />
<br />
不久前，美国财政部长盖特纳上任以来首次访华。媒体称，盖特纳此次中国之行扮演的是一个推销员的角色：向中国兜售的，是近来急剧膨胀起来的美国国债。《纽约时报》就说，中国是否愿意继续购买美国国债，是盖特纳此行的“心头大事”。<br />
<br />
金融危机的爆发让中国一跃成为美国最大的“债主”：截止2009年3月，中国所持美国国债总额高达7679亿美元，（如今最新的数据显示已经是8千多亿美元）占美国可流通国债余额的13.5%。这些巨额的美债资产，面临美元贬值、债券价格和收益率下跌带来的双重损失。　　<br />
<br />
从某种意义上来说，正是由于中国的财力支持，正是中国不断地购买美债与美元，美国才能有财力把它的航空母舰游弋在这个世界的每一片海域上。也因此，这么“富裕”的中国才会穷的造不出一只航空母舰。　　<br />
