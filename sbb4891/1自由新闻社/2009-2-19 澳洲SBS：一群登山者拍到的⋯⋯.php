													<h2>这么著名的事件，干嘛神秘兮兮的？</h2>
						
						
																											<div id="postmessage_211178" class="t_msgfont">囊 帕 拉 枪 杀 事 件<br />
维基百科，自由的百科全书<br />
<a href="http://zh.wikipedia.org/wiki/%E5%9B%8A%E5%B8%95%E6%8B%89%E6%9E%AA%E6%9D%80%E4%BA%8B%E4%BB%B6" target="_blank">http://zh.wikipedia.org/wiki/%E5 ... 0%E4%BA%8B%E4%BB%B6</a><br />
中国边防部队和被拘捕的难民在卓奥友登山大本营<br />
被射杀的Kelsang Namtso<br />
<br />
囊 帕 拉 枪 杀 事件是指2006年9月30日中国边防武警向企图违反中国法律穿越西藏与尼泊尔边境上的 囊 帕 拉 山 口（海拔5700米，一译朗喀巴山口）、出境前往尼泊尔的75名西藏逃亡者开枪射击并至少打死两人的流血事件。<br />
<br />
逃亡者中包括年幼的儿童和两名带路的向导。2006年9月30日，据目击者和逃亡者称，中国西藏边防武警总队日喀则大队定日中队在没有预警的情况下，向这些试图徒步穿越山口的藏人开枪射击，17岁（有报道称23岁）的Kelsang Namtso（女）被子弹击中，在山口前死亡。另一名23岁藏人Kunsang Namgyal（男）被两次击中腿部后倒下，由武警带走，事后中国当局承认Kunsang Namgyal死亡。<br />
<br />
中国当局声称，士兵开枪是出于“自卫”。这一声称与现场西方目击者的陈词有矛盾。事后，41名幸存者抵达位于尼泊尔首都加德满都的“西藏难民中转中心”。两周以后，幸存者抵达目的地印度达兰萨拉。<br />
2006年9月30日，逃亡藏人正在翻越囊帕拉雪山，中国边防军向逃亡藏人开枪，被登山者拍到。<br />
三个逃亡的拉萨青年<br />
<br />
文/萧抟牵9月29日&#8226;明报世纪版）<br />
<br />
编按: 经历了拉萨镇压事件、北京奥运，苦难的藏民竟摇身一变，被指控为暴狂民族。或许只有以下这些甘冒性命危险、攀山越山岭赴抵印度难民中心的逃亡故事，藏民才能被证明活在汉化、种族边缘化的政策下，被证明活在自己的文化根源地而有如难民。<br />
<br />
从西藏逃亡至印度，是一条踏上攸关生死的叛逆路途。<br />
<br />
每年，仍有千名藏民，翻越海拔七千米的喜玛拉雅山，跋涉至尼泊尔或印度。<br />
很多藏人在寒冷的冬季上路，大风雪会减低遇上边防军警的机会；却要忍受强风暴雪。曾经，一名年轻尼姑，在越过海拔五千八百公尺的朗喀巴（又译“囊帕拉”）山通道时，在雪地上中枪。年前，三个拉萨青年，选择走上这条路。<br />
<br />
在印度北部的达兰萨拉，一所简陋的藏族外语学校内，遇见三位来自拉萨的青年，<br />
他们过着另一种生活，渐远离拉萨的岁月。<br />
<br />
二十五岁的旦秋，自中学毕业后，一直没法在拉萨找到理想的工作，作为藏族人，她感到地位不如汉人，待遇较好的工作都先聘用汉人。在没有前景的日子里，她一边挣扎，一边计划逃亡。零六年一个天气微寒的十月，她安排好路费给边境带路人，没有向家人道别，一个人悄然上路。<br />
<br />
从西藏至印度，有两条路。一是取道江孜、帕里、亚东关口，翻越惹嘎拉山口到锡金，二是从日喀则经樟木到尼泊尔，大部分藏民选择第二条路。<br />
<br />
旦秋说，那天清早，她从拉萨到达西藏边境樟木，早前联络的带路人一直未有出现，她只好等待，一夜又一夜，直至第四日，带路人终于找到旦秋，他们连忙上路。上山的路险峻，但为免被发现，她们不时躲躲藏藏，最后还是遇上边防总队的军警，她和带路人被抓到部队的派出所。<br />
<br />
对于被拘留的情境，旦秋仍然印象深刻，她回忆说，踏入派出所，她看见旁边漆黑的大房子内挤满被捕的逃亡者，原来当天共抓了四十一人,房间没有空位；她和带路人被关在另一小房子，她的手被扣在窗上，军警向她问话，如不回答，便用手上的对讲机打她，连续拷问了一夜，旦秋的颈已被打至红肿。第二天，她被带到山下另一处地方，带路人已被捆绑在一间小房间内，军警用电棒打他，他已站不起来。旦秋被安置在一个小客栈，等待遣返日喀则。<br />
<br />
一名藏族老人负责看守旦秋，过了数天，老人暗示如拿出四千五百元，可以送她到尼泊尔边境。旦秋表示，她一直希望到印度，只好答应。老人安排车子让她去边境，沿途遇上不少军车，幸好没有人发现，直至靠近边境山脚，她被赶下车。<br />
要到尼泊尔，必需要跨过眼前的一座山，旦秋手脚伤势仍未好转，山上的气温较低，她不知道能否熬过去，向前的路比后退的短，她惟有向前走。沿路碰上一些逃难的人，她跟着大家前进的方向，一直走了七小时，终于看见尼泊尔边界。<br />
<br />
<br />
卓玛自去年五月逃至印度，为免家人受牵连，一直未与拉萨的母亲及兄长联络，因为电话有窃听，她怕出事。未到印度前，二十二岁的卓玛，一直在宾馆工作，她形容，大部分在拉萨的藏族青年，没有考上大学，很难跟汉人竞争。藏民没有完整的西藏教育，她没有学到正确的藏语及西藏历史，学校教育以汉语为主，她希望得到传统的藏民教育，去印度成为她唯一的愿望。<br />
<br />
逃亡对藏民是危险的事情，几乎没有回头路，卓玛表示，一开始没想过要这样做，她只是一边工作，一边申请护照，然后到印度进修，但两年以来，护照没有批下来，在拉萨也没法过好日子，她才选择逃亡。卓玛自言颇幸运，她透过相熟的人找到一张到樟木的通行证，从那里到尼泊尔边境，只需爬过那座山，及花二千元带路费。<br />
<br />
卓玛的母亲，一直反对她离开拉萨，认为这样太危险，如被军警遣返，以后她在拉萨更难发展。卓玛说，她当时只想尽快到印度学习，没有考虑太多问题，包括从印度能否回拉萨。<br />
<br />
五月一个清晨，卓玛乘车到樟木边境，她的通行证令她顺利进入尼泊尔边界，很快她便找到带路人，然后跟随他翻越这座山，往尼泊尔。卓玛说，沿途她遇上一些逃亡的人，大家为怕军警发现，一路躲躲跑跑，途中，曾被远处军警发现，带路人迅速带她藏至丛林，最后她们摆脱军警追踪，完成这六小时的山路。辗转卓玛由尼泊尔再到印度，最后定居在北部的达兰萨拉。<br />
<br />
<br />
现年二十岁的普布，来自拉萨一个贫穷藏族家庭，从少失学，帮助家人干粗活维生，直至十六岁，他知道没有念中学，也不懂汉语，不可能找到工作，惟一的出路是逃到印度，到那边接受免费的藏族教育。零四年一个冬天，由于没有任何路费，他和二十七名藏民，从亚东关口，攀越喜玛拉雅山到印度。<br />
<br />
回想起那段艰苦旅程，普布还印象难忘，以为会死在山上。十二月的喜玛拉雅山，气温严寒，晚间可降至零下十五度，普布一行二十八人，选择在这段时间逃亡，纵然可以避过军警的拦截，但也有不少人没法走完条路。普布说，他们从未爬过海拔七千米的喜玛拉雅山，而且还要在寒冷的晚上步行，以免遇上边防军警。白天，他们躲起来睡觉，天黑才继续上路，有时雪地冻得没法步行，他们是互相扶持，一个挨一个，一直走了十多天，路程还未够一半；普布犹记得两名拉萨女孩，脚上受冻伤，没法走下去，只好让她们离队，从此便失去她们的消息。<br />
<br />
普布说，大队走了一个月才到达印度边境，他们大部分脚部已冻伤，走到后段时，他们的脚已包扎重重烂布，几乎蹒跚上路，他们勉强撑下去，并以为会死在山上。普布表示，那时是过得一时得一时，能否活下去已没有把握；直至有一夜，他们看见印度边境的军人，才知道抵达目的地，但经历多日的饥寒，他们已没有了知觉，直至到达达兰萨拉，普布才回到现实世界。<br />
<br />
曾经问普布，如果让你的人生重来，你会走上这条逃亡之路吗？<br />
他说，藏人生来便别无选择。<br />
<br />
希望学校<br />
<br />
在达兰萨拉一处唐У胤剑二十一岁的校长丹真与几名青年，创办了一所希望学校，这群青年有个愿望，是令所有藏民都能接受教育，并多学几种语言，让他们更融入世界。创办人之一罗布，是来自拉萨的一名二十七岁牧民，06年他从拉萨逃亡至印度，他不满意中国当局，未能重视人权，尊重藏民的宗教自由，及西藏文化，所以在此设立属于藏民的学校。罗布表示，三十岁的藏民，不能接受常规教育，这所学校为他们带来希望，可以继续免费进修，现时有一百多名学生。<br />
<br />
在这所学，遇上很多从中国各地逃亡过来的藏民，包括拉萨来的旦秋、卓玛和普布，他们每天都在这里上课；不少外语教师，是从欧美到此做义工的外国人。<br />
卓玛说，她在这里学习英语多个月，迟些还会担任汉语老师，感觉特别充实。<br />
<br />
今年，西藏发生事故，边境驻守严密，几乎没有人敢逃亡至印度。据达兰萨拉难民收容站的数字，每年平均有二至三千人，从西藏逃亡至印度；但08年显着下降，直至九月只有四十多人，平安抵达印度。<br />
