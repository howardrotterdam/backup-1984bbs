																											<div id="postmessage_648748" class="t_msgfont">打造政治犯家属的历史群像<br />
<br />
王丹<br />
<br />
<br />
1996年我被中共当局以“阴谋颠覆政府罪”判处有期徒刑11年。宣判之后，送到辽宁省锦州监狱服刑。<br />
<br />
为什么是锦州监狱？当局至今没有给过任何明确合理的解释。从法律上讲，按照中国相关的在押犯人执行条例，被判刑者一般应当在其户籍所在地的监狱服刑，而我的户籍所在地就是北京。1989年之后我第一次被捕坐牢，前后转了三个监狱和看守所，也都是在北京。显然，送到锦州是违反了有关法律条文的规定的。从政治上讲，我的案件受到国际关注，属于比较敏感的政治案件，会不会是因为当局不希望我在北京关押，会引起更多政治上的麻烦呢？显然也不可能。因为监狱是严密的封闭空间，我又是受到特别管束待遇的犯人，除了直系家属，任何人不能探视我，其实我是否关在北京，都无妨当局封锁任何有关我的消息。那么，到底是为了什么，当局要把我送到千里之外的锦州服刑呢？<br />
<br />
想来想去，唯一可能的解释就是，当局就是为了直接惩罚我的家人，以便间接惩罚我。换句话说，服刑地点的选择，目的只有一个，就是加重惩罚。<br />
<br />
当局心理很清楚，我家人也再三表示过，不管把我关押在哪里，家人都不会放弃每个月探视我的权利。因此，故意送到山海关之外，冰天雪地的东北，能够达到的效果只有一个，就是使得这样的探视变得异常地艰辛。后来的事实也是如此，在关押在锦州监狱的一年半左右的时间里，我家人每个月从北京坐火车北上，到锦州再换乘公车，手里还提着各种送给我的物品，包括沉重的书籍，但是依然风雨无阻地前来探视。尤其是我的母亲，经过1989年之后的反复折磨，身体状况非常不好，但是十八个月，每个月这样长途跋涉，这样的艰辛对她的身心都是极大的折磨。而折磨她，就是当局的目的，否则找不到任何合理的解释。<br />
<br />
现在回想起来，我都还会百思不得其解，当初那些做出这个决策的官员，不管他来自中共的政法委，还是政府系统的司法部，他们也是三月怀胎长大的有着母亲的人类；眼看着无法以判刑来打击我的意志，就用迫使我家人承受这样的艰辛以确保每个月能够见到我的方式来加重惩罚，人性何在？这是一个什么样的政治制度，可以培养出这种人面兽心的家伙，居然还执掌法律制度的实施？中国的大好河山，落在这样的人的手里，真是中华民族的不幸！<br />
<br />
我母亲再版这本《岁月苍苍》，就是为了记录下这一段历史，就是为了让后人可以看到：当年，那些为了追求民主，却失去自由的人，在他们的背后，还有他们的家属，曾经与他们一起并肩承受苦难，一起迎接厄运。而我的家人的故事，就是中国的政治犯家属的历史群像的一部分。这本回忆，我也看作是打造这座历史群像的庞大工程的一部分。<br />
<br />
回顾人类历史上追求真理的道路，有很多的政治犯因为牺牲而载入了史册，但是他们背后的家属群体，是不容忽略的。从原沙皇俄国时期十二月党人被流放时，家人的陪同流放；到台湾美丽岛大审之后，政治犯的妻子们集体代夫出征投入政治犯对运动；从丁子霖老师组织天安门母亲们持之以恒地抗争，到师涛的母亲只身远赴联合国人权会议，为儿子大声呼吁。没有这些家属的支持，历史的这一页就不是完整的。<br />
<br />
尤其是今天，我们看到这样的历史还在延续之中。尽管中国政府力图营造一个大国崛起的形象，但是胡佳的妻子曾金燕，刘晓波的妻子刘霞，陈光诚的妻子袁伟静……这个政治犯家属的名单依旧在不断扩充之中。曾经发生在我家人身上的那些事情，尽管已经过去了十几年，但是丝毫未改地发生在她们的身上。中国的变化有目共睹，但是这些没有变化的东西，难道我们就可以视而不见吗？今天再版我母亲这本回忆，既是记录我们一个家庭的故事，也是为了记录一个时代的过去，更是为了昭示中国的未来：只有那一天，当中国再也没有那些母亲，妻子，丈夫，孩子，仅仅因为他们的家人在思想和言论上不能为政府所容，就要承受巨大的苦难的时候，我们才可以说，这是一个值得我们为之骄傲的国家。<br />
<br />
最后，我谨代表我母亲，再次感谢在我两次入狱的过程中，对我，对我母亲，对我全家给予了勇敢而温暖的关怀和帮助的所有人。你们，也是这本回忆，以及这一段历史的书写着。<br />
<br />
