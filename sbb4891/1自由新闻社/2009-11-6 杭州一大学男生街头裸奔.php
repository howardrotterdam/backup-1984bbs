<br />
昨天有人裸奔了！晚上9点，在下沙，裸奔者是个大学生。<br />
<br />
事情发生得很突然。据目击者称，晚上9:07，在学府街下沙最繁华的地段之一，刚下课的大学生刚刚蜂拥出来，买夜宵、逛店铺、看摊位、散步。人群中突然开进一辆蓝色福特车，一个浑身赤裸、腰间挂一块白色小布的男生从车里钻出来，在路中央狂奔起来。<br />
<br />
人群立即沸腾了！路的两边站满了人，很多地摊被围观人群踩翻了。<br />
<br />
裸奔男生以百米的速度冲刺，跑了50多米后，他突然一把扯下腰间的白色小布，以完全赤裸的姿态转身冲回福特车，站在车门后面。男生举手向人群示意，说了一句话：“谢谢大家。”<br />
<br />
一个卖小饰品的中年摊主是现场最靠近福特车的人，他最早目睹了男生下车，似乎有点不敢相信：“真是裸奔么？我好像看到一个人什么都没穿跑了过去。”得到周边人肯定的答复后，摊主说：“太丑了，太丑了，怎么可以裸奔！”<br />
<br />
周围恰好是大学生的生活区，数百人围观，有人在楼上大喊。也有人说，“被雷得外焦里嫩”。<br />
<br />
围观者之一李男(化名)说几天前他就从网上听到这个消息了，特地借来一台DV机：“这种视频一定要留下来的。”<br />
<br />
福特车开走了，还有学生在现场一脸迷茫，到处拉着人问：“真的裸奔了？太快了，我什么都没看到。”<br />
<br />
记者搜索了一下网上的资料，这有可能是杭州城里第一个裸奔的大学生。<br />
<br />
“裸奔男”是谁<br />
<br />
人群中，有人在呼叫“胡元华”(化名)。<br />
<br />
现场确认，胡元华就是裸奔男，东阳人，下沙一大学国贸专业学生，今年即将毕业，家境中等。<br />
<br />
为什么要“裸奔”<br />
<br />
胡元华语：<br />
<br />
我愿意为大家示范什么叫激情；<br />
<br />
在这么沉闷的环境里，能够娱乐大家，娱乐自己，也是始终不易的。<br />
“裸奔男”其人其事<br />
<br />
想找到“裸奔男”并不困难，不惧“裸奔”，还怕见人？<br />
<br />
尾随着他的哥们，在宿舍里找到他。<br />
<br />
他一头板寸，一幅黑框眼镜，个头1米7多一点，有点微胖，样子不算很帅。<br />
<br />
这是一个怎样的人？<br />
<br />
他自己说：进大学以前，他是一个不完全意义上的好学生。<br />
<br />
初中时，他拿过全校第一；高中时，班主任评价：“活动能力很强，学习不错。”<br />
<br />
偶然也有惊人之举，比如打过几次架，半夜翻墙去网吧。他的高中女友张依然(化名)觉得这都很正常，“男生都那样，他只是更张扬一点，傲一点，大胆一点而已”。<br />
<br />
但是听说这个曾给自己写过几首诗的男生裸奔，她觉得“有点疯狂”。<br />
<br />
他的兄弟说：在大学，他不是一个完全意义上的差生。<br />
<br />
读到大三，总共59门课，他35门课没有通过，按照学生手册的规定，他已经很难毕业。<br />
<br />
据说，大一那年在几乎没上过课的情况下，他花了4个小时翻了一下《进出口贸易实务》，结果通过了考试，自此对大学考试彻底失去信心。大学后两年，他基本没进过教室。<br />
<br />
据说，他曾经被辅导员钦点为班长，后来因为旷课、成绩差、常抽烟，引咎辞职。同学反映，他工作方式简单直接。<br />
<br />
开车送他去大街裸奔的是他的哥们，“他为人直率、豪迈，一般情况下很理性，碰到自己感兴趣的事情又会很感性”。自修课时，他们曾经一起翻墙出去，一路狂奔尖叫，也许只是去喝一碗粥，就会满足地回来。<br />
<br />
室友阿海和玉峰对他的裸奔一点都不惊讶，“谁裸奔我都奇怪，就他我不会奇怪。哪怕他真去非洲打工了(大三结束时，胡元华曾打算去非洲的莫桑比克闯荡)！”<br />
<br />
他们对胡元华的评价：奔放，有思想，不随大流。虽然成绩差了点，但是比谁都像个知识分子。<br />
<br />
他寝室的书桌上，放着《卡夫卡悖论集》、《论悲剧的诞生》、《海子诗集》、《人与永恒》等。<br />
<br />
他会写诗。半夜，他常常以诗或杂文抒发自己的愤懑。一位同学母亲的同事曾表示，只要他的诗歌达到足够数量，愿意帮他出版。<br />
<br />
诗友兼哥们阿森用海子的“以梦为马”形容他，这位通过互赠诗歌认识的朋友评价他的诗歌：“充满了爆发力。”<br />
<br />
同在一个学校读书的女生“猫猫”说，他是“我活了二十多年里，见到的最有浪漫情怀，最理想主义的人”。<br />
<br />
有一天，他的辅导员在食堂吃饭时突然想起一句话“别人笑我太疯癫，我笑他人看不穿”，他觉得用在胡元华身上正合适。<br />
<br />
他曾劝过胡元华去上课，却无效，此后大多同他聊人生、聊音乐、聊教育。<br />
<br />
<br />
“裸奔男”并非第一次裸奔<br />
<br />
有人透露，这不是胡元华的第一次裸奔。<br />
<br />
今年更早些时，为安慰失恋的兄弟，他就在学校生活区裸奔过一次。当时所穿的白袜子还保存着，上面沾满杂草。那次他有点喝高了。<br />
<br />
这一次，他准备得很充分。<br />
<br />
事先在网上请网友帮忙分享文章，号称“多少人分享，就跑多少圈”；<br />
<br />
公开征集裸奔时的口号，坐车到裸奔地点防止半路被拦下……<br />
<br />
很明显，他希望这件事情能扩大影响。<br />
<br />
与“裸奔者”的对话<br />
<br />
问：你对大学的感受是什么样的？<br />
<br />
答：有点失望。<br />
<br />
问：为什么不选择离开？<br />
<br />
答：没考虑过退学，我在大学里自学得开心。这样父母也不会担心。<br />
<br />
问：还想毕业吗？<br />
<br />
答：我还是抱有幻想，在特长能够被别人认可和欣赏的情况下，获得特许毕业的机会。如果校长告诉我在哪里发表多少文章就可以毕业，我会去努力。<br />
<br />
甚至于，学校如果给我个毕业证，上写“不准胡元华靠本证求职谋生，只准其父母证明胡元华已大学毕业”，也可。<br />
<br />
问：为什么选择裸奔这种形式？<br />
<br />
答：我觉得学校很沉闷，我愿意为她来示范什么叫激情。<br />
<br />
我喜欢尝试没做过的事。如果我可以做到不在乎其他人的目光，某种程度上，我已经得到了自己的认可。<br />
<br />
再不济，能够娱乐大家，娱乐自己，也是始终不易的。<br />
疯狂背后，或许是渴望被关注的心<br />
<br />
从传播学的角度来讲，裸奔确实是一个仅以个人行为就能达到大众关注的最具“性价比”的方式。<br />
<br />
自称性格孤僻、偏爱半夜写诗的胡元华，说起话来却滔滔不绝，时不时抛出一堆理论与名人说辞。<br />
<br />
