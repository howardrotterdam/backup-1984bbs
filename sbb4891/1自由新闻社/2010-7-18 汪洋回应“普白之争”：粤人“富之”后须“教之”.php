													<h2>汪洋回应“普白之争”：粤人“富之”后须“教之”</h2>
						
						
																											<div id="postmessage_746466" class="t_msgfont">　　普通话（华语）与白话（粤语）之争在广东进一步升温，地方政府推广普通话的意图明显，但广东人却不愿妥协，纷纷站出来捍卫白话。<br />
<br />
　　广东省委书记汪洋昨天在广东省委十届七次全会上说，广东人均生产总值（GDP）已近6000美元（8300新元，下同），可以说基本“富之”，下一步更重要的是要“教之”，以教开智，以文“化”人，通过教化提高人的素质。<br />
<br />
　　汪洋引述《论语》故事说：孔子到卫国看到熙熙攘攘的人流，弟子冉有问，这地方人口众多，该如何治理？孔子答：“富之”；冉有又问，富裕起来之后又如何？孔子答道：“教之”。<br />
<br />
　　至于如何“教之”，汪洋提出诸多论述，其中包括要引导民众多看文艺演出，参观博物馆、美术展等活动，提高情趣与文化素养。所谓“教之”，也包括推广普通话。<br />
<br />
　　这是自广州近日爆发“普白之争”以来，广东最高领导人首次发表的看法。<br />
<br />
　　广州市政协近日正式向广州市长万庆良建议，广州电视台的两个主要频道改用普通话。书面建议中说，把广州电视台的综合频道或新闻频道改为以普通话为基本播音用语的节目频道，或在其综合频道和新闻频道的主时段中用普通话播出，以满足今年11月来广州参加亚运会和旅游的国内外宾客语言环境的需要。<br />
<br />
　　在解释以上建议时，广州市政协提案委有关负责人说，广州的外地人众多，这群人的权益也应该受到重视。此外，广州还面临着国家中心城市的建设，以白话为基本播音用语，就无法上卫星，也就无法发挥国家中心城市的作用，白话明显与这些新形势不适应。<br />
<br />
　　“普白之争”目前也波及到广东其他城市。东莞石碣纪念园的袁崇焕雕像，基座上原刻有他带兵打仗时的口头禅： “ＸＸ妈！顶硬上！”，这句口头禅近日已遭东莞文化部门下令铲除。<br />
<br />
　　“顶硬上”是袁崇焕当年保护明朝京城的主旋律，而“ＸＸ妈”是骂明朝昏君的广东粗话。 “ＸＸ妈”被视为白话特色方言，广东军人在作战时，都喜欢把这句粗话，当成冲锋口号。该事件引起舆论和民众强烈不满，令白话I卫战火上加油。<br />
<br />
　　有网民说，袁崇焕的口头禅不止是广东文化，也是中国历史，当局没有理由铲除这句口头禅；为了推广普通话而牺牲广东文化和中国历史是不负责任的。<br />
<br />
　　另一方面，广州一班80后青年则仿效香港年轻人，走上街头进行“我为粤语歌曲狂”的活动，高举“广府话起锚，煲冬瓜收皮”的标语，并快闪“唱粤语歌”，表达对白话被“扼杀”的不满，力争保留广州文化。<br />
<br />
　　除了青年，广东名人、全国人大代表、曾任多届全国政协委员钟南山院士前天也说，广东要建设文化强省，应该力挺白话。<br />
<br />
　　籍贯福建但在广州长大的钟南山前天在自由呼吸中国行广州站活动上被记者问及“普白之争”时说，当然要力挺白话。广东要建设文化强省，白话有助于发扬地方文化，又能促进同乡、华侨交流，拉近彼此感情，对发展地方特色起到重要作用。<br />
<br />
　　他说：“粤语不单是语言，更涉及到广东文化、广东人喜怒哀乐的感情，我不赞成用普通话取而代之。”<br />
