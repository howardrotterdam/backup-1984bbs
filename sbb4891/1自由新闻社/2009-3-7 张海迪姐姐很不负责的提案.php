													<h2>张海迪姐姐很不负责的提案</h2>
						
						
																											<div id="postmessage_46870" class="t_msgfont">　　信息时报3月7日报道 “张海迪，快！”昨日，在全国政协十一届二次会议分组讨论会上，中国残联主席张海迪甫一露面就受到媒体追逐。张海迪将向大会提交“允许更多残疾人驾驶汽车”的提案。<br />
<br />
　　张海迪告诉记者，科技新成果和人性化的设计不断应用于汽车工业，驾驶汽车越来越简便。很多人实现了驾驶汽车的梦想，而残疾人同样有驾驶汽车的愿望。他们也应该有与健康人同样参与社会生活的权利。今天科学技术的进步为残疾人驾驶汽车创造了条件。我国港澳台地区和一些发达国家，残疾人驾驶汽车十分普遍，一些国家和地区还允许残疾人从事出租车行业。根据国际惯例，以及人们不同残疾的程度，通过安装辅助装置，很多残疾人都具备了驾驶的汽车能力。我个人也学会了驾驶手动汽车，2005年还曾参加两岸三地残疾人驾驶汽车神州行的活动。<br />
<br />
　　张海迪说，根据第二次全国残疾人抽样调查统计，我国有8296万残疾人，其中肢体残疾人2412万、听力残疾人2004万、言语残疾人127 万。随着我国经济社会的迅速发展，一些残疾人的生活水平不断提高，部分残疾人具备了购买汽车的能力，要求放开残疾人驾驶汽车政策的呼声日益强烈。2003 年，公安部出台了允许左下肢残疾人考取小型自动挡载客汽车驾照的规定，迈出了允许残疾人驾驶汽车的第一步。但是政策的开放仍然是有限的，除左下肢残疾人外，包括右下肢、双下肢、上肢残疾和听力言语残疾在内的残疾人依然被排除在外。<br />
<br />
　　张海迪建议公安部切实加快工作进程，积极与有关部委研究解决相关问题，逐步放宽残疾人申领驾驶证条件，修改《机动车驾驶证申领和使用规定》，尽快出台右下肢、双下肢残疾人和听力语言残疾人驾驶汽车政策，切实维护残疾人的权利。 (本文来源：信息时报 作者：记者 薛冰 朱小勇 黎泳芝)</div>

							
							
							
							
