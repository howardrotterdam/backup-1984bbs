林浊水近照<br />
<img src="http://i25.tinypic.com/qqsqk5.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
本文作者郭宇宽近照<br />
<br />
在大陆的背景，即使知道从书本上得到的关于台湾历史的知识，也很难理解，为什么这么多台湾人希望独立，像228事件这样的问题，在我看来并不是要独立的充分理由。我们眼里台湾人和我们都是“龙的传人”，可他们却觉得和你不是同胞。有几次，包括在纽约，在有台湾人的场合，我习惯地说“我们中国人”，会被纠正，“对不起，我不是你们中国人，我是台湾人”，甚至不愿意跟你讨论，为什么台湾人不是中国的人的问题，这是非常让人沮丧的。<br />
<br />
当然我也怀有自己很深刻的偏见，是希望天下亲如一家，更不要说分裂，更不要说战乱。也许和我的文化熏陶有关，我有一定程度文化意义上的大中华意识。就像甘地想把巴基斯坦留在印度的愿望一样，在我看来，哪怕让台湾来统一大陆，把台北设为大中华的首都，都比两岸分裂要好。不过我有一个信念，不把自己的观念强加给别人，如果需要影响别人，要以耐心的说服而不是拳头和武力，是文明人的常识。<br />
<br />
在台湾访问的时间里，我拜访了一些台湾的知识分子和贤达，我记得我那个困惑，我都让他们推荐，在台湾，谁能最完整和严密地阐释，台独的逻辑，给我讲清楚，为什么台湾人不是中国人？所有的人都跟我推荐一个人,他就是林浊水，他是“台独理论家”，是台独纲领的起草者,甚至说他是台独的精神领袖也不过份，更重要的是，他被包括国民党的人在内公认为是一个非常待人诚恳的君子。<br />
<br />
有人警告我，他是一个非常雄辩的人，你要小心一点，而我只怕他不够雄辩。我不会因为不同的观念，而先入为主把别人当作敌人，我把倾听和自己不同的观点当作一种习惯，不仅要听别人说话，而且要有勇气听人家的真心话。甚至我相信未来海峡两岸能否统一，就是取决于通过经过经常性的相互观念刺激，两岸能够培养将心比心，推己及人的善意。<br />
<br />
当联系林浊水先生的时候，他很爽快地答应，并约定了时间。那天他穿着颇有品位，皮鞋一尘不染，我们的讨论是坦诚和开放的，甚至针锋相对，而他始终保持优雅的微笑。<br />
<br />
<br />
郭：你是从小就有强烈的台独意识么？<br />
<br />
林：不是，我小时候糊里糊涂的，受国民党的教育也是大中国意识，还要去解放大陆受苦的同胞，只有少数知识分子，在争取民主的过程中，涉及到台湾主体性的问题。直到美丽岛事件那段时间，是一个转折，在那前一年美国和中华人民共和国建交和台湾断交，我们一下子就有一种孤立感，危机感，大中国的迷梦彻底被粉碎了。<br />
<br />
郭：怎么讲？<br />
<br />
林：我们这些打拼的台湾人，在国际上居然没有一个主体性可以代表自己，说我们有中华民国正统，代表中国吧，中共不承认，而且把我们逼出联合国。那我们台湾人是什么？我们更不愿意中共来统治我们，我们只好自己代表自己来独立建国，建一个台湾人自己的自由民主国家。<br />
<br />
郭：可是恕我直言，你姓林，这个姓大概也是从福建过来了，从血缘上讲我们也不是异族，为什么要分成两个国家呢？<br />
<br />
林：有人说人类的起源都是在非洲开始的，那是不是说全要变成一个国家呢？要说是华人去开拓了就算中国的领土，那新加坡也主要是华人开发的，为什么不算中国的一部分？<br />
<br />
郭：我其实并不认同我们官方所说的“台湾自古就是中国不可分割的一部分”这种反智的说法，但是以我了解的历史资料，在清代，中国对台湾建立了生效的主权统治，虽然一度割让给日本，但这和新加坡还是不能比的。<br />
<br />
林：把台湾并入中国，是在清帝国，清帝国不能等于中国，爱新觉罗式，对于中国来说是异族统治者，否则你可以拿蒙古统治中国的时候说话，当年蒙古的版图都算成今天中国的么？<br />
<br />
郭：满族今天已经完全融入了中国，而且清朝的版图和以汉人为主体的中华版图也是有历史延续性的，这和蒙古人全世界性的征服不一样，但是和如果那元代的统治来类比，就比较好一点。虽然也有例子，近现代的俄国，无论是斯大林的苏维埃还是沙皇俄国都侵占了中国的很多领土，但是那个历史非常复杂，如果中国人民那时候有力量主宰自己的命运，是不会答应的，而且更难接受让这样的事情重演。<br />
<br />
林：我能够理解，中国在近代史上受帝国主义的欺负，这种委屈使今天的中国有很多要强大的愿望，而且有一种帝国思想。我觉得这是很遗憾的。中国不是号称以马克思主义为意识形态么？马克思还有列宁都讲民族自决啊，按照马克思主义，就应该让西藏人自己表决？应该让西藏独立啊？更不要说，台湾中华民国一直以来就是一个实际的国家，我们有自己的军队，邮政，外交，中共的政权从来没有一分钟对这里行使过主权。北京政府一会儿用马克思主义，一会儿又用帝国主义，根本没有一个统一的逻辑来讨论问题。<br />
<br />
郭：不好意思，我不是代表中共政府，我只是作为一个中国人的情感来认知，中国被割地的记忆对民族心理的烙印是一个现实，所以哪怕一个普通的老百姓都希望国家统一，就像分开越久的一家人越希望团聚一样，对普通中国老百姓来说，这种团聚是很灵活的，是共产主义去统一，还是三民主义来统一，都区别不大。你可以看到连战这样一个在台湾可能被人看起来挺平庸的一个政治人物，在大陆访问，特别是在南京中山陵，受到民众自发的欢呼。即使在我这样一个倾向自由主义的大陆知识分子看来，能够在一个民主的制度框架下，比如用联邦制这样的形式，两岸打破樊篱，绝对是一件好事，全球的华人我想绝大多数都会感到高兴。<br />
<br />
林：我觉得没有必要要和在一起，你知道英国主体是盎格鲁萨克逊人，在美国独立时，也都是盎格鲁萨克逊人为主，还有加拿大，澳大利亚，新西兰，也都是盎格鲁萨克逊人为主，现在都是独立国家，这些独立国家和英国之间，也都有很友好的关系，经济上的交流和人员的流动都很顺畅，在外交上还经常相互支援，不也是很好么？美国和加拿大今天也没有说一定要合并到一起啊。<br />
<br />
郭：我想英国和美国，澳大利亚之间的独立故事，恐怕不大可能发生在大陆和台湾之间，英国和美国，澳大利亚离得太远，那里土地又太广阔，很难实现有效的管理。所以权衡之下英国放弃，如果像大陆和台湾之间这样隔岸相望，我相信当年大英帝国也决不会容忍美国独立。<br />
<br />
林：我想今天的国际规则，也不是中国想怎么样，就可以怎么样的。台湾今天是民主社会，可台湾人还是愿意并服兵役，这说明什么，不是台湾人喜欢当兵，而是这说明台湾人为了维护自己的独立，愿意当兵。当然打仗是谁都不愿意的，但我相信如果一定要被逼在被北京政府统治和战争之间选择，我想很多台湾人不会退缩。<br />
<br />
而且我不相信中共的实力就大到可以为所欲为，如果能那样的话，他们早就这么做了。<br />
<br />
郭：我个人的看法是绝对不愿意一定要在分裂或者战争之间作选择，但是很多大陆人也许也会愿意打仗。我想能不能在战乱和一种比较好和睦的相互尊重的状态下来统一之间选择，这样不用形成大陆来征服台湾人的感觉。今天的世界，除非有宗教或者种族不可调和的矛盾，实在不能一起相处下去，否则分裂不是这个时代的主题。像欧共体，过去不是一个国家的现在都要和在一起，历史上已经是一个国家的，今天了何必再要分开呢？<br />
<br />
林：这个不大一样，欧洲现在都是民主国家，而中国的政治制度你是知道的。而且今天的欧盟已经实现了统一市场，货物，劳动力都可以自由流动，而大陆今天省和省之间货物还不能自由流通，你们还有户口限制，农村人都不能进城。中国人之间都没有平等的公民权，没有政治参与的机会。台湾的中华民国护照，在世界各国实际受到尊重的程度，要超过中华人民共和国。台湾为什么要加入中国？<br />
<br />
郭：我承认你说的有一部分是实情，不过情况在变化，你说的省和省之间的贸易壁垒，那大概至少是十几年前的事情了，大陆现在市场经济发展还是比较迅速的。农村人今天进入城市不像过去那么困难，甚至可以找到工作，我们现在最大的问题，农村户籍人口还不能享受到公平的社会福利，还有在城市的政治权利。这些让人很着急，但趋势一定是在向改善的方向发展。而大陆的政治体制进步，我相信就像台湾80年代开放党禁，报禁一样，那是不可阻挡的历史潮流，很多人都在为此奋斗。<br />
<br />
到那个时候，你觉得是不是大陆和台湾统一的恰当时机?<br />
<br />
林：可能是我的信息有些落后了，我四年前写的一本书是这个观点。不过我想大陆要民主化也不容易，假如要二十年的话。再过二十年，台湾和大陆作为两个政治实体已经变100年了，国际通行法则就是要尊重既成事实，为什么还要统一呢？台湾独立的愿望是一个历史的累积。那统派报纸联合报的调查来说,90年代初台湾要求统一的60%以上，要求独立的只有10%以下；2000年要求独立的在50%以上，要求统一的只有20%，统计表示，马英九上台并没有改变这个趋势，只不过大多数人不希望主动挑起战争而已。我想到20年以后，台湾人会更加坚定地相信我们是一个独立的国家。而且我想假如中国能成为一个民主国家，有了新闻自由，中国人应该更能了解台湾是一个独立国家的现实，会接受这个现实。<br />
<br />
郭：我想这种状况是不大可能发生的，在你看来，台湾和大陆作为两个政治实体已经100年了，但在大陆民众的立场来看，台湾从来就是中国的一部分，从来没有改变过，这也是一种情感的历史积累。如果一定要独立，即使大陆民主化了，大陆的大多数民众我想也不会答应。我想可以类比的是美国的南方要求独立，最后造成南北战争，骨肉相残，这种悲剧不要发生才好。<br />
<br />
而另一种选择，统一对台湾地方政府还是人民都有好处，我指的是在中国建立民主和地方自治规则的情况下，台湾人还是可以选择自己喜欢的领导人，用不着担心大陆派一个你们不喜欢的人来统治。而且台湾的政治精英还可以到更广阔的政治舞台上去发挥，我注意到台湾因为多年民主熏陶，政治人才的素养超过大陆和港澳，未来假如台湾有马英九这样形象口碑都好的人物，大家选他做大中华联邦的总统也不是不可能啊。更直接的对台湾老百姓的好处是，两岸成为一家，台湾就不用再维持几十万的常备军和高额军费。台湾的年轻人不用再必须当兵，省下来的军费，可以增加社会福利，这样对台湾每一个老百姓都有好处。稍作权衡，就会觉得统一对大家都好。<br />
<br />
你觉得到那样一个状态，你愿不愿意放弃台独的主张？<br />
<br />
林（沉思的大约30秒）：我认为台湾是一个独立的国家，这是在我身上几十年历史积累的认同，我想我这个年纪自己已经很难改变了，我个人的观念选择我想也是应该被尊重的。但是，假如真到了你说的那一天，我不能预料，我会保留自己的情感,尊重台湾人民的选择。<br />
