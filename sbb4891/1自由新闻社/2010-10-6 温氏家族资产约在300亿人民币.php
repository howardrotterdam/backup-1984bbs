<br />
旭光：温家宝政改背后的故事<br />
<br />
中国总理温家宝已经成为这个东方大国“政治话题的制造者”。他在深圳向邓小平像献花圈，有人解读为借“邓小平南巡” 来宣示改革意志；他重复了邓小平一句“不改革就是死路一条”，被解读为释放最强烈的改革信号；就连他随便说说的建设法治政府，也引动一些人浮想连翩，认为这是温家宝在自己管辖的国务院领域有效推进体制改革的重要信号。就连在温的八年内变得更加苦难深重的访民，也主动展开了学习温家宝讲话的小热潮，搞起了中国政治的“行为艺术”――这当然是民间的一种揶揄。<br />
<br />
温家宝已俨然成为中国知识界的“可卡因”，只要他一点火，知识界就跟着激动；温家宝已经炼成了独门的点穴神功，手指一挥，中国知识界的“羊角疯”就犯了，整个网路就变成了幻想、意淫的天堂，就连老牌民运都发出“温家宝讲话是骗人的吗？”的反问，在那儿帮温家宝数钱玩儿。按何清琏的说法，温家宝掀起了中国知识界一场“比傻游戏”。<br />
<br />
倒不是何女士刻薄，已经有人发出“挺温就是训虎”的梦呓，以为自己训虎功夫了得，可以凭借“与虎谋皮”，把老虎关进笼子里――中国知识界的想象力，真是任何时候都不可低估啊。<br />
<br />
但事实恐怕是，虎皮没有谋得，却捧着温家宝抛来的画皮，在那儿空欢喜一场，而抛皮者却借这众多的现场观众，来个“金蝉脱壳”，溜之乎也。那么，温家宝的葫芦里，究竟装着什么药呢。其实，温整个的秀场，不外乎为了三件事。<br />
<br />
一，引开公众对经济社会发展注意力，掩盖过失。<br />
<br />
事实已经证明，一个处于社会转型关键期的大国，将主管经济的总理之位交给一个完全不懂经济、只懂权术的政客手里，其结果可能是灾难性的。就连对温报幻想的人，对温任期最后两年内能作些什么，都不报太大的希望。虽然任期未满，但八年已经足以给温家宝盖棺定论了：中国这八年虽然表面光鲜，但实际上进一步深陷危机，社会处于随时崩溃的状态。自十七大以来，对于温经济政策的批准不绝于耳。现在看来，温政府的经济政策可能是中共改革开放以来最糟的，不仅在改革的关键点上大步倒退，对前期改革积累的问题没有解决，甚至进一步放大了过去的问题，并将改革的机会丧失殆尽。<br />
<br />
过去改革的最大问题就是一部分人先富起来后，没有带动共同富裕。中共是充分认识到这一点的，所以胡锦涛才强调民生的重要性，强调改革成果让大多数分享。但强调归强调，既得利益集团在过去的改革中萌芽，到了温政府手中，却进一步壮大、固化，成为吸附在中国民众身上最大的吸血鬼，整个国家已经权贵资本主义化，温家宝也成为围绕其家族的特殊利益集团的核心人物。无论温的手下，包括国资委、财政部、统计局怎么说的天花乱坠， “国进民退”都是不争的事实。原先由邓小平启动的放水养鱼，搞活民间的改革，在温手中彻底逆转为“与民争利”。<br />
<br />
垄断利润越来越高，前国资委主任李荣融卸任时透露，7年来，“央企的资产规模由7万亿元增加到21万亿元，7年来央企累计上缴税金达到5.4万亿元”。与之相应，事关百姓生活的衣、食、住、行的成本，八年来翻了好几十倍，医疗、教育、住房成为压在中国百姓头上的“新三座大山”。而百姓收入增长却极其缓慢，中国国民的总工资在国家收入中的比例，从改革前的40％已经下降到14％左右。<br />
<br />
这里不说让人惊心的房价了，仅医疗一项，就可看出中国百姓的苦痛无奈。前些天，中央电视台将中国某些地方药价下降了 30％作为药品改革的成就在那儿炫耀，但他们就是不算算，自温政府以来，药价涨了何止300％。每公布一次药品目录，药价就上涨一次，据新华网报道，仅 2009年11月底人保部公布最新版国家医保药品目录这一次，前后数十种目录中的药品涨价，最高涨幅竟达到近90％，一些仿制药的价格甚至超过进口药。<br />
<br />
这一切都是温政府八年来糟糕的经济政策造成的。所谓墙内损失墙外补，越是不懂经济，经济越失败，百姓越苦不堪言，温家宝就越强调政治改革，就越发把头仰得很高，就越做无奈状，将大家的目光从经济发展上引开，把责任推向他的前任和顶头上司以及下属，制造一个“有心杀贼，无力回天”的印象，赢得社会的同情，掩盖其过失。<br />
<br />
二，掩盖因弄权而激起的官场反感，弹压反对声音。<br />
<br />
事实也已经证明，温家宝是中共权力最大的一届总理。据香港媒体报道，现在国务院的要害部门皆由温家宝的亲信主持。如发改委一把手张平，深得温家宝信任，任国家发展改革委副主任不到半年就被调至国务院任副秘书长，负责国务院办公厅常务工作，专门为温家宝服务。2008年 3月，温派他接替马凯任国家发展和改革委员会主任。发改委副主任兼能源局局长张国宝，是温氏家族在能源领域利益的保护者，所以虽早已过了退休年龄，但温家宝就是不让他退。<br />
<br />
国土资源部徐绍史，与温一样是搞地质出身。温把他调入中央，历任国务院办公厅秘书一局副局长、局长，是温担任副总理时期的大秘书。2000年12月任国务院副秘书长。2007年温提拔他担任国土资源部部长、党组书记，兼任国家土地总督察。这个土地掌门人，与温家宝之弟温家宏成为“房产大亨”之间的关系，不言自明。<br />
<br />
工业和信息化部部长李毅中。2008年任新组建的工业和信息化部党组书记，是温家族在信息产业上的代理人。温云松在银行、证券公司的业务拓展，以及与国际知名厂商戴尔、北电和思科的合作等等，背后都有李毅中的影子。其后Unihub并入电讯盈科与中国电信的合资公司，李毅中也出力不少。<br />
<br />
审计署署长刘家义。十七大时，温通过明升暗降的手段挤掉爱抢他风头的原审计署李金华，把刘家义扶正。刘家义旗下的审计署，是温查整国务院系统、国企老总及地方上那些不听话官员的先锋队。只要温对谁不满意，审计署肯定会第一时间去审计，找出问题后，由温再斟情定夺。<br />
<br />
在温的权力手腕之下，其他部长也大都看清形势，对温亦步亦趋以保官位，国务院系统已基本成为温氏天下。温在人事上的强硬姿态，彻底打破了中共人事提拔任用的惯例，人事提拔任用由“潜规则”变得“无规则”，或者干脆成为“温规则”：国务院的人事，胡锦涛基本上处于管不了的状态，温家宝说提谁就提谁，温家宝说不让谁上谁就上不了。<br />
<br />
温当然知道这样会激起官场的不满。他的手段就一个，对外营造自己的改革形象，使那些本来对他人事问题上任人唯亲作风有意见的人，因有所忌惮而收敛。因为反对改革是当今最大的一顶帽子，谁都不敢戴到自己头上。<br />
<br />
三，掩盖家族腐败，保护家族利益。<br />
<br />
中共红色家族中谁最富？据外媒相关信息透露，当数温家宝家族。随着政治权利的扩张，温氏家族已经完成财富的原始积累成为当代中国第一富户，也是当今世界所有的总理中，或者说中国自从100多年前共和以来所有的总理中，最富有的家族。<br />
<br />
外媒列举的事例显示，温云松的私募基金发展迅速，已完成21个投资案例，个人资产少说也在100亿人民币左右。另外还拥有中国平安保险的73.6亿，和Unihub约有30亿股权。“珠宝女王”温夫人张蓓莉至少拥有市值约319亿的“戴梦得”20％股权。若再加上温家宝弟弟温家宏数十亿的资产，温氏家族资产至少约在300亿人民币（以下均为人民币）左右。<br />
<br />
温氏家族的腐败传言流传越广，温家宝做秀的需求就越大，劲头就越足。因为他知道，只有民望和美誉，才能最终确保他家族的利益。他头上的光环越多，形象越复杂，别人就越不好动他。<br />
<br />
胡锦涛是个难得的厚道人，但胡的缺点也很明显，就是作为中共一把手，其底线是不能让红色江山在自己手里变色。温家宝完全摸清了这一点，这么多年来自己通过做秀赢得的巨大民望，已经令胡锦涛等中共常委望而生畏，无可奈何。于是他越来越放手施为，几乎达到了想怎么干就怎么干，想怎么说就怎么说的地步。当温一旦拿出谁胆敢查我家族，大不了大家一起完蛋的架势，胡锦涛和其他常委，就只好就范，几乎已无法有效制约温家宝。他们担心，如果对温家宝真的形成不利局面，对方就会借其营造的个人形象，对外制造打击改革派的印象，可能农民和中小知识分子就会不答应，就会影响社会稳定，同时国际舆论也会产生不利影响。这也正是温长期刻意做秀的真正目的。<br />
<br />
可见，亲民、普世价值、政治改革等等，都是温家宝有意扔出的一张张画皮，扔得越多，他身上的保护层就越多。目标只有上述三个，其中又以最后一条，即保护家族利益为核心，这就是温家宝葫芦里的药。</div>

							
							
							
							
