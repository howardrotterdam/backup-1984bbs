我的小学<br />
黄章晋 <br />
<br />
　　到学校报道那天，我是骑着一根扫帚一路跳到学校的，那是1977年一个阳光灿烂的上午，那年的夏季格外漫长，全都是阳光灿烂的白天，整个夏天，我始终保持着双手紧背在腰杆脊背挺直的姿势，听校长解释什么是学生，听班主任点名，听班主任发表一些我记不清内容的讲话，以及在教室外学习列队，学会打扫卫生。也许这是一天发生的，或互不相连的几天内发生的，但30年后，我总把它记成是整个夏天发生的事。<br />
　　这个记忆肯定是不可靠的，我后来想起，其实是我妈带我去报的名，有个细节我忘不掉，老师在填写一个表格时问我的“成分”，我清楚记得，我妈尴尬而又带着讨好的微笑贴近老师，悄声说：“是地主。”这个结果我以前就隐约知道，我一看我妈的神情，就立即做顽劣状，假装被别的东西吸引而没听到“地主”二字。<br />
　　也许报名时我还测试了数数，似乎我表现得差强人意，因为我性格一贯羞涩，之所以对此依稀有印象，是我数数识字之类早慧表现一直是我父母的炫耀资本，我记得我们班上有同学甚至读到了二年级依然不能数到一百，报名时则大部分人无法过关。<br />
　　――必要交代一下，我出生在新疆建设兵团一个偏僻的农场里，那时成年人十天才算一个礼拜，能有一天休息，1977年以前，每天大家下班吃完饭，便是雷打不动的开大会政治学习时间，父母不太可能有过多的“私有时间”去教育孩子，我们的长大，没有外婆的童话、没有妈妈的儿歌陪伴。我四五岁时能流利地数到一百，大半得归功于我6岁才有一个妹妹。很多父母甚至忙得都忘了给孩子起名，直接由学校老师现场发挥，我们班一位女同学和她在隔壁班的哥哥就是一起拥有正式学名的。<br />
　　现在想来，报道那天我骑着的那根扫帚肯定是不存在的，我们家的孩子在父母面前似乎是从来不被许可乱蹦乱跳，那根扫帚应该是正式上课好几天后的事情。<br />
 　　　　　　一<br />
　　我的班主任是语文老师，姓王，中等身材，圆脸，齐耳短发。小学作文里，无论是老师还是妈妈，外形描述一定是这样，因为有篇写老师课文就是这样的开头。在我们眼里，全世界的女性都是这个样子。如果我们的教材不是从那时起每年至少一变，没有每年几何级增长的出版物，我想，直到今天，我的汉语里也绝不会出现“高挑”、“修长”、“娇小”之类描述女性身材的美好词汇，至于圆脸，应该是那时中国人认为最好的脸型，在一个物资匮乏的年代，也许16比9的脸型才是最能代表富足和幸福吧，30年过去，现在最好的脸型已是巴掌大的小瘦脸，我们的汉语似乎还没及时发明出形容它的美好词汇。<br />
　　虽然我完全不是那种善于讨好老师的学生，但我很快就在某些时候成为班主任的宠爱对象，特别是在别的老师和校领导听课时，我会成了她的撒手锏。因为我是个语文天才，这个留在后面细说。<br />
　　班主任的性格，我的谨慎表述是，喜欢突然发火，有时候她的发火会要全班心惊胆战莫名其妙。她喜欢说，要是在旧社会，老师打学生是可以用棍子把学生的手都打肿，此说与我父母的说法一致，所以，她偶尔用教鞭、粉笔头，或者干脆就用拳脚教育一下学生，大家都觉得太正常不过，因为所有班主任都是这样的。<br />
　　我是那种一直不需要老师施以颜色就天然敬畏老师的学生。我曾长期为一些哲学问题所困扰，譬如，我们的班主任也会在厕所拉屎撒尿么？她会和她丈夫吵架甚至被她丈夫殴打么？那时，她在我心中的神圣程度完全与毛主席相等，就如我一直想不出毛主席是否以及如何上厕所一样，我的想像力是难以将老师这样“形而上”的概念与“形而下”的事物联系在一起的。<br />
　　不过，我很早就开始在家里兴致勃勃地对父母讲我又发现我们老师在哪里讲错了。――后来我才知道，我们那所兵团子弟学校的大部分老师其实都只文革时初中毕业，我开始上学那年，即使学校临时动员那些出身不好的人当老师，也拼凑不起一只真正合格的教师队伍。<br />
　　帮助我对班主任完成“祛魅”的，是隔壁班的语文老师，一次她到我家做客聊天，开玩笑说，你看你们王老师“抱窝”都刚好选在放假的时候，了不起啊。<br />
　　天，她居然用母鸡孵小鸡来形容我的班主任老师生孩子！<br />
　　我们的数学老师换了很多个，只有一位在一二年级时短暂教过我们的老师，我能清楚记得她的模样，她姓邵，其余的不但面目模糊，甚至连到底有几位、姓什么我都记不清了。<br />
　　我之所以记得她，完全是因为她的形象，现在想来，她应该很像香港明星邝美云，她的穿着仪态完全迥异于周围，她软软的南方口音，她的一举一动都令我觉得心头有把小刷子轻轻刷过，后来我才知道，那种东西是“气质”。我试图了解她，但此意图被班里一位母亲是老师的同学很不屑地及时打断：“切，她老头子是个臭老九。”邵老师很快就从我们的学校里消失，后来我模糊知道，她一家都迁回上海了。<br />
　　数学老师少有明显喜欢我的，因为我对只有正确答案而没有更正确答案的数学几乎毫无兴趣，也因为数学课我才知道我原来有异常严重的走神习惯，语文是课本一发下来我就能开课前就看完，走神不走神无所谓，但数学课走神，碰到质数合数奇数偶数这些概念我就经常会弄混，而四则混合运算没完没了的重复练习令我视为畏途，只有碰到异常复杂的应用题，我才突然兴趣大增，甚至老师没讲到的课我也会兴致勃勃地做个没完。<br />
　　有一个学期，我对数学作业的厌倦到了无以复加的程度，几乎每天早晨都无法交上数学作业，不过，我那时已经交了几个坏同学，上学路上就对好口供，说我把数学作业本拉到他家了。这样的谎言很难天天重复，而且，同桌的女同学又特别积极，我还没撒谎，她就大声嚷嚷我根本就没做作业，然而，我忠厚老实的外形和面不改色的即兴撒谎天才显然能要善良的老师更相信我，相比我出色的成绩，举报者的一贯表现和成绩很容易就在老师那里落下个嫉妒的印象。<br />
　　人是很容易在记忆里悄悄美化自己的，我甚至觉得小学时的表现真够的上天才了，我画画比中学生水平高，成年人能想的出来的任何国家，我都能随口告诉他首都是哪里，至少，我小学成绩从没掉出过前五名应该是相当可靠的吧。<br />
 　　　　　　二<br />
　　我们班主任有时会疯狂表扬我。原因很简单，能考满分的学生不算太稀奇，但是用“在……上”造句，我一出手就显示出其他学生所无的想像力：“英明领袖华主席领导全国各族人民在新长征的道路上继续奋勇前进”、“伟大领袖毛主席在天安门城楼上向全国各族儿女亲切挥手”。<br />
　　没有什么造句能难住我。譬如，“一边……一边”，一位班干部要老师激动不已的造句是：“在放学的路上，同学们一边走一边唱着欢快的歌”，而我，一张嘴就是“英勇的边疆各族儿女，一边辛勤劳动建设边疆，一边手握钢枪保卫边疆”――我相信我们老师绝造不出这么金光闪闪的漂亮句子。<br />
　　我记得有次老师为准备一次漂亮的听课，悄悄把我和几个班干部叫进她的办公室面授机宜，当时新课文将学到“亡”这个生字，学生要组词，老师绞尽脑汁好歹给我们每人都想了一个带“亡”的词，在一群“灭亡”、“死亡”、“逃亡”的学生中，老师问：“还有什么亡？”我答：“伤亡。”我肩膀立即落下重重一掌，“你真行！”因为“伤亡”这个词在当时的口语和广播里几乎完全不存在。临到现场发挥，我的“伤亡”令听课老师惊讶的嘴还没合拢，再次庄严举手：“老师，还有一个词，亡我之心不死。”<br />
　　在一次课堂作业时，班主任悄悄对听课的副校长这样解释我的惊人表现，他爸爸妈妈都是大学生。毛主席保证，我当时被老师的公然撒谎吓得笔都快掉了，因为我们团场里，我只知道有一个人是大学毕业生，而我父母都只勉强读了五年书。<br />
　　如你所知，我造句能造成这样，等开始写作文，自然我比那些只能造出“放学路上我们一边走一边踢着小石子”的同学高明太多，因为我太知道什么样的东西是老师最喜欢的，而且我无师自通地知道描写一次有意义的劳动课，哪些该详写哪些该略写，什么时候该及时地检讨自己的偷懒然后幡然悔悟，什么时候该及时地插入对祖国四个现代化实现后美好生活的憧憬。甚至班里同学最后作文的结尾流行用省略号，也是我的发明。<br />
　　不过，我因语言能力被班主任宠爱的时间没能延续贯穿整个小学，大概是三年级时，我们家订阅了《湘江文艺》，每篇文章我都会认真看完，结果，我对穷尽脑汁的胡说八道逐渐失去了兴趣，老师的表扬激励被模仿真正汉语表达的冲动完全压倒，那些金光闪闪的东西突然就从我的作文里消失了。<br />
　　许多年后，读到郑渊洁的文章，我才清楚找到那种潜意识抵触的最直接表达：我的整个小学语文教育，其实都在教我们撒谎，都是对我们优美的母语的污染。当时我不但作文水平退步了，我还嫉妒其他作文写的好的同学，譬如老师在上面念学习委员的范文，我会突然插嘴：“抄的！抄的《小学生作文选》！我看过……”即使被叫到办公室训话，我依然不服气：“……捧在手里的金鱼哪里会眨眼呢？鱼又没有眼皮，我保证她根本没见过真的金鱼……”千真万确，我们那里鲤鱼都没有，别说学习委员，班主任都是没见过金鱼的。<br />
　　我有时会无端地想，当年被我嘲笑的笨家伙的经历，如果被挂到我的头上，那么今天回忆起来，其实是颇能显示从小就特立独行的证据。比如他们哭丧着脸被老师拧着耳朵带到十来平米大小的花坛：你再仔细看看，难道这个花园不能要你联想起祖国美好的未来吗？看到这些花你能不想起辛勤的园丁，想到老师么……然而，我很多时候却是个讨巧的迎合者<br />
　　我无意给读者留下我们老师还是满脑子文革思维的印象。有两件事我能清楚记得班主任的态度并感念她．一次是作业中“千万不要忘记阶级斗争”这句语录少了个“不”字，老师愤怒地说，现在是赶上了好时代，要在以前，你爸妈都要被连累劳改！还有一次，是当时随着彭德怀陶铸平反，我父母在家整天与要好的朋友谈刘少奇会马上平反并诽谤伟大领袖，我不知此事轻重，给最要好的哥们大谈毛主席是个最坏的家伙，但因为我们某天吵架翻脸，第二天老师就知道我散布了反动言论，她在办公室对我的教育非常简单：你说没说过这样话？没说？没说那就好，你现在知道不能随便乱说了吧，我警告你，不要把你从家里听到的东西随便给同学说。<br />
<br />
　　　　　　三<br />
　　我读书的年龄在现在看甚至是偏大的，但在当时，班里几乎少有比我年纪小的同学，因为各家庭孩子多，即使有托儿所，孩子多的家庭为省钱，也是大孩子照顾小孩，这导致普遍很晚入学，我隔壁一个要好的伙伴比我大一岁，为了在家带两个弟弟，他比我还低一年入学。<br />
　　计较同学们的年纪大小是有道理的，当时风气很糟糕，我五岁时曾无缘无故被一个骑自行车的学生追逐碾压，你可以想象我们那里小孩打架斗殴之风有多盛行。小孩打架比发育，差半岁就容易分出胜负来，而我在高中前一直是豆芽菜型体格，更糟糕的是，我只有一个妹妹，对那些有不止一个哥哥的家伙，我嫉妒得要死。有一年我父母说湖南老家有个表哥要来投靠，我盼了他半年，甚至早早放出我将有靠山的风声。我表哥无比强悍，甚至能平提两桶水，但他是个28岁的人了。<br />
　　似乎整个小学时代，在校里校外我都是被欺负的对象。<br />
　　也有例外，兵团子弟学校有地方上的少数民族学生，我们班里就有几个，有个个子有一米七的同学名字我还现在记得，刚开学数学老师问他名字，他特别胆小羞涩，大家总听不明白，他一个眼睛特别大而机灵的同伴代他大声念出名字：哈勒斯别克！听到这么奇怪的名字，大家哄堂大笑。起哄就注定了他们在学校受欺负的命运。他们经常被班里最捣蛋的学生围殴，哪怕是才打过架的同学此时都会一起联手。他们几乎不反抗，此时，那些平时受欺负的倒霉蛋也会乘火打劫。我虽没参与班上欺负少数民族同学，但参与过路上向外班的少数民族同学扔石头。也许是语言原因也许是被欺负的原因，少数民族的孩子没有一个能坚持读完小学的。<br />
　　如果今天还有我当时那样的学校，估计没有父母肯把孩子送那去。我们没赶上不爱红妆爱武装的时代，但还保有遗风，热天，男生人手一个弹弓或铁丝编造的弹弓枪，寒假一过，则弓箭风行。三年级时，一位许姓同学开学那天被隔壁班飞来的一枝箭射中眼睛，没多久，隔壁班一位同学被高年级同学的火药枪射中，五毫米铁丝截断做的子弹到医院才被取出。相比之下，我眼睛和额头在混战中被飞来的红砖碎块击中挂花各一次，可算受伤次数最少的学生之列，因为我一直是很老实的孩子。读四年级时，学校终于出了一桩当地轰动一时的人命案，五年级一个专门的留级班里，一位智力上有问题的同学在野外把一位低年级同学用铁丝勒死了。<br />
　　女生打架，我只记得学校有过一次，一位很早发育的女生把欺负她弟弟的小男生揍得满脸开花，闻名一时。总体上，女生是完全不同于男生的生物，她们爱聚在一起跳皮筋，当时的跳法好像都是以上海、北京这样的大地方命名。班里长的特别漂亮的女生最容易受欺负，她的课桌里经常会被无缘无故塞上一大堆垃圾，看她被羞辱得跺脚哭泣，那个做了坏事的小男生会挤在起哄的人群里特别开心，恶作剧是我们那个时代情窦初开的小男生掩盖和表达爱意的惟一方式。<br />
　　有一个同学我偶尔会想起来他今天怎样了，他是我们校长的儿子，在隔壁班。我们校长是个轮廓分明一脸青色胡茬的大男人，但他的儿子却是个秀气的小女生，是的，他说话是女孩的声音，他走路是女孩的姿势，他从不与男生玩耍，从来都聚在女生堆里跳皮筋或踢毽子，他在学校里非常醒目，在身着鲜艳衣服的女生堆里，唯一平头灰蓝色男生装，拍着手笑着跳着的就是他。<br />
　　问题是，在当时，他的女性化，无论是在他的父亲还是老师们眼里，都似乎是被鼓励和赞美的，是啊，如果我们都像他，这个学校是多么太平无事啊。那个空气里还没有细菌的年代，他的父亲是绝不知道有同性恋一说的。他当时几乎很少正眼看我们，其轻蔑之意，甚至比那些最受宠的女生还强烈，而几乎所有的男生不但从不取笑他的女性化，反倒自惭形秽。<br />
　　真的，他现在怎样了？<br />
 　　　　　四<br />
　　要么是我的记忆出了问题，要么是我的班主任对我的宠爱的确只在局部时刻，否则无法解释，像我这么好的学生居然到了三年级都没戴上红领巾。当时，我们班里那些成绩勉强算中等的同学甚至一些留级生都入队了，没入队的，要么是不可救药的老留级生要么就是无法无天的坏家伙。<br />
　　我们班第一批同学入队时，当时好像还叫“红小兵”，我以为论成绩和表现无论如何该有我的份，然而入队者，除了极个别老实善良得带有苦大仇深状的同学外，其余全是班长学习委员小组长之类，这些人之所以成为班干部，其实是因为班主任以前就认识他们的父母。当然，只要不是脑袋特别笨，一个孩子有了班干部头衔这样一个巨大正面激励，无论如何都是可以成绩名列第一梯队的。<br />
　　等头几批同学被班主任钦定入队后，我在班主任那里的局部宠爱已超过了所有人，看上去。然而，第一次轮到我入队时，突然开始搞民主了。令人尴尬而意外的是，在投票时，我票数竟然不够，尤其令人愤怒的是，一些平时似乎很要好的同学在不同意我入队时，手举得老高，甚至炫耀性地从座位上半站了起来，十几分钟前他们还和我打的火热，那情景要我想起哪儿看到的一句诗：“升起了梯也尔无耻的旗”。<br />
　　我现在都没想明白，当时那些表现最好的同学――老师标准――几乎都投了反对，好像是商量好的，而那些中不溜的围观群众或坏家伙都投了同意。反对的理由高度一致：最近很是骄傲自满。证据：前几天因为看了《抓壮丁》，把那顶解放军帽的里子翻了过来，里边撑了一根柳树枝，弄成了一顶国民党军官的帽子，在教室里大摇大摆炫耀。令人发指的是，那几个指控我的男同学几乎全部都跟着我干了同样的事。<br />
　　这是我一辈子经历过的惟一一次民主投票。 <br />
　　早几批同学入队时，我日思夜想能脖子上红领巾呼啦啦被风扯着四处招摇，有回做客，我还乘大人不注意，溜到主人里面房间把墙上挂的红领巾绑脖子上，对着镜子照了小半天。到后来，入队问题的心病越来越重，一者无法给父母解释，二者实在不甘于差生为伍。至于红领巾的荣耀，早随着班里一大半人入队而无所谓了。<br />
　　终于有一天，上学路上，班长突然赶上来搭着我的肩语重心长地说，一定要好好表现一番，你看谁谁都入队了，你怎么能不进步呢？这种小屁孩说成年人的话，当时可把我温暖了好多天，当时我激动得只会乱点头。班里通过我入队时，已经没有什么投票不投票了。反正已经是四年级，小学毕业前，大家都得入队。于是，我终于顺利地成了光荣的少先队员。<br />
