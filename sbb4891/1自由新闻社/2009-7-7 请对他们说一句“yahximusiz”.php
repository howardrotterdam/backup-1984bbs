请对他们说一句“yahximusiz”<br />
作者：黄章晋<br />
<br />
　　湘女的稿子也许明天后天能看到。从当时想着要去，到稿子出来，心里一直悬着，复杂古怪的感觉。甚至当时头儿耐心说服我一定不能去新疆，我还悄悄长舒一口气，我很怕积郁了太多东西，真正仔细写，万一脑袋一片空白，什么都写不来呢。<br />
<br />
　　当时听到《八千湘女上天山》这个选题时，我脑袋直摇。倒不是这个同名长篇报告文学我已看过很多年，而是要真正做好它太难，做不好不如不做。在我心里，它是个如此艰涩的话题，如一条冻僵的蛇，需要在胸膛里慢慢暖醒，然后，它立即对着你心头猛咬一口。我有两个姑姑就是当年八千湘女的一员，一位早已过逝，活着的那位依然在生活中挣扎，她们始终未曾回过故乡，我从没见过她们，她们的消息我所知甚少，而我姨妈不知算幸运还是不幸，当年独自背着行囊坐在宁乡县城通往长沙的大道上等着接女兵的军车，半个多月后失望地回家，与传说中的俄语学校、女拖拉机手、女会计绝缘。<br />
　　<br />
　　湖南经视正在热炒“湘女回故乡”，我们要做这个选题，正是湖南经视希望配合宣传，代理我们来回路费。给家里电话，母亲说，虽然湖南经视的主持人要她觉得太浅薄，令她直想骂人，但她还是每每看得情动难以自抑，她是６０年代作为盲流自己跑到新疆去的，毕竟不是政府集体组织骗去，自然永无资格进入历史，当然，他们也没经历过前一代人那般沉重的苦辛，那种投身天堂却掉进地狱的磨难。毕竟八千湘女那一批，大部分人当时都还未成年。<br />
　　<br />
　　既然大家一致同意说要做这话题，我终于热乎起来，强烈要求参与其中，我说，还有人会写这个题材写得比我更好的吗？于是，那头驻湖南的记者邓飞迅速有了回音，经视可以负担我的路费一同去采访。<br />
<br />
　　我才知道这个话题在湖南被湖南经视搅热到了什么程度：听说，经视将专机接一些代表回来，而热烈响应号召自费驾车重经当年八千湘女路，去接老湘女回乡的市民已准备了上千台车，此活动的结尾，是在新疆寻得一饱经沧桑的巨石立在长沙湘江边上做一巨大的纪念碑。它的确一开始就是个商业操作，甚至带着我强烈不喜欢的《激情燃烧的岁月》的气味，但它更的的确确搅动了许多人的――我不知道该怎样使用这个字眼，总之，无论是什么旋律，我相信这个纪念碑至少还远没北京某报发动建一座“抗非典胜利纪念碑”那么无耻。架势摆好，唢呐一响，离人再远，眼泪也被乡音催落。多年前贡献出自己的女儿，今天有人记得，无论纪念时被迫赋予其什么意义，但在中国这块总是习于忘掉自己儿女的土地，总归是桩破天荒的事啊。<br />
<br />
　　我个人总怀疑，“湘女回故乡”能热到这个程度，除了她们独特命运中有打动人心处外，多少还因为，对新疆这块土地，部分湖南人心理上会有比其他地方的人有更深厚的情感渊源，虽然今天新疆移民中来自湖南的人口比例远低于北方其他省份。但最关键时代的确与湖南人有关，先是左宗棠、刘锦棠收复新疆设省，有了“大将筹边尚未还，湖湘子弟满天山”之语，自后的新疆和平解放，起义者警备总司令陶峙岳和接收者王震都是湖南人。据说，当时全疆汉族不满１０万，革命军人却有２０万，本以为解放了全国人民，该解放自己，该“楼上楼下电灯电话”了，谁料竟要在此打一辈子光棍，于是，便有王震向湖南的黄克诚要女兵，有了八千湘女上天山。<br />
<br />
　　我父亲一家能追溯到的历史，正从左宗棠西征开始。据说一位先人自小从军，数十年后孤身从新疆返湘，形同老丐，侄辈中惟有一家未嫌弃他。后侄孙善读书，家贫无力负担，老头便摸出腰刀割开军靴，取出藏匿多年的金条，父亲一家由此开始翻身。不过，自后两代人读的全是军校，第二代人大都在同乡陶峙岳麾下服役新疆，１９４９大兵西进，他们竖旗“咸与解放”，随后全部成了犯人。再后来，是我父亲在家乡立脚不住，手执陶峙岳亲启的信笺跑到新疆，未几，光杆司令陶峙岳靠边站。<br />
<br />
　　等我父母到新疆时，当年湘女的孩子早已能背着书包上学堂了，而且各省青年男女源源不断补充进来，湖南人早被稀释得不成比例。甚至，我母亲刚在一个连队立脚时，她竟是那里的第一个湖南人。众人道：“呀！湖南妹子，厉害啊！”于是，我母亲便听到了第一个当年湘女的故事。主角是长沙一大户人家的女儿，刚落脚就美得惊动了师首长，首长三天两头开着吉普下到田里慰问，但该女冥顽不化，任各级领导死活做不通工作，而首长也表现出惊人的布尔乔亚耐心，不是亲来就是秘书写信表示要组建革命家庭。有回首长携随员到农田探望，浩浩荡荡一干人马跟上，该女竟不理不睬，首长便亲剖一西瓜进前双手捧上，该女接过西瓜便劈头砸在首长头上。首长一生征战杀人无数，竟大庭广众被一黄毛丫头羞辱，怒从心起，当场拔枪将该女击毙。首长后竟免于刑法，只关了段禁闭后被降职调动，死者妹妹得信，千里寻仇，声言不偿命便要亲手执法，终于惊动党中央，至于那位不幸的首长，是到另一个世界继续追求还是留在阳世永失自由，大家已记不清了。<br />
<br />
　　今天看到关于湘女的报道，几乎全是“组织介绍，个人同意”，终于幸福的故事，尤其喜欢重点介绍当年积极分子、今日功成名就的八千分之一。也许对多数人来说，真是当年那些“我比胡杨泪更多”的往事，她们不愿向人提起，甚至，“最难夜夜梦家乡，想爹娘，泪汪汪，遥向天山，默默祝安康。既是此身许塞外，宜红柳，似白杨”也早成了往事。她们那一代人的时运不济还落在第二代人身上，因为等到１９７８年时，他们的子女早已成了农场职工，既不可能通过升学回到故乡，更不可能举家迁回原籍，不再把自己作为国家祭品的指盼，只能落在孙辈身上。<br />
　　<br />
　　我家一位老邻居是王震、王首道抗战末期从浏阳招的老兵，随王震入疆。说起当年，他曾介绍，有人眼见将孤身终老戈壁而绝望自杀。第一批女兵抵达时，狼多肉少，中下级军官连女兵影子都没见着，牢骚比以往犹甚，于是便有动员大会，刚做新郎的大老粗首长大手一挥，豪气干云：党中央毛主席说话算数，有些革命军人瞎说只有首长才有老婆，不对！毛主席说了那就一定会做到，放心，老婆肯定会每人发一个的。<br />
<br />
　　也许是人在讲故事时，希望举极端的例子，老先生口中说的“发老婆”是实在的“发”，女兵们按指标就近分到需要的首长身边，刚一抵达，首长们便忙着收获，至少并不全是《激情燃烧的岁月》里的做思想工作，因为新疆的“女人荒”是如此的严重，因为“批判的武器”未必总是有用，所以，关键时刻还得靠“武器的批判”：“不同意也得同意，门一反锁，你想，都是些女娃娃，能反抗得了么，第二天大家就上来道喜。”老先生还笑言，还有为了免得首长之间互相挑选争抢，就如公平发放战利品一般，人刚一到就抓阄，抓到谁是谁。这里有多少为增大传播效果的语气夸张，我不知道，但当年听人反复说起这段历史，几乎全是“一个阶级推翻另一个阶级的暴力斗争”的例子，少有“ 绘画绣花”式的“雅致”和“从容不迫”。心智未开的时代，初听陈年旧事，竟未如今天想来这般惊心动魄。――只在偶有一次听说，有一车女兵被少数民族土匪给半路截走，“我们”的“军需品”竟然成了他们的“战利品”，那种愤怒呵。<br />
<br />
<br />
（当年，她们风华正茂）<br />
<br />
　　事实上，作为战利品或后勤装备的，远不止“八千湘女”，先后还有很多省份的女兵被配备给当地军人。现在的说法，当年奔赴新疆生产建设兵团的知识青年以数十万计，多少人是因此而被招去的。那位老先生的太太就是山东人。有的说法是，最开始是在山东招女兵，因为战争造成该省性别比例严重失调，许多寡妇也一并招来，被送往新疆的，除了年轻女学生，甚至还零星有北京、上海等地被改造的妓女。不过，越到后来，军婚的自由程度越高，不复当年惨烈。其他省为那里贡献了多少女儿，手头没资料，那一批人，除了湘女，似已无人再记得她们。<br />
<br />
　　我大抵能想象得出今天还活着的湘女在年轻记者递近的话筒前，该是怎样的反应。她们的命运恰如今天中国的命运，虽是强行成婚，一年年苦熬过来，少年青春被戈壁风沙染白了双鬓，那人成了丈夫，成了孩子他爸，接受下来，接受下来，一切便成了我们的历史我们的一部分，父性的话语便左右了可怜的母亲的记忆母亲的感知。把强迫当成坦然的争取，便在心理上摆脱了苦难，这还并非心理对苦难的适应，更有外在无时不在的灌输之功，官方的话语强权最终会内化为她们的自我认识。湘女的子孙会谴责他们父母的婚姻么？<br />
<br />
　　只有我父母那一茬稍晚些的盲流，今天才会毫无“献身边疆”之类的大词，因为没有欺骗没有强迫，没有“认命”，只有“活命”。政策松动时，能迁则迁，不能迁则一定要子女考上大学离开这里。<br />
<br />
　　命运，就是无处可逃。如果我现在问母亲，当年刚刚中学毕业的姨妈最终没成为八千分之一，是幸还是不幸？也许她会深思半晌说，无所谓幸与不幸，因为姨妈很快嫁到一个遥远的矿山成为矿工家属，即使不曾早逝，几十年后再看，并无好坏之别，甚至，在新疆意味着会有更多可能，因为在那个时代，那种家庭出身的人，别无选择。某一天，等我给自己的孩子说道历史，会告诉他，民族的悲剧时代，不仅是一颗大树被伐倒，还有每个家庭、每个个人，像树上每根枝条、每片叶子被揪下来，打在泥水里。<br />
<br />
　　所以，５５年过去，即使少数后来设法回到故乡或离开新疆的“八千湘女”，闻知今天的活动，想来也会感动莫名，哪怕她今天依然在某个时候悄悄认为那是不堪回首的苦难，但依然会在这一刻留恋那早已解体但却总可为人生意义有个交代和安慰的话语。至少，苦难总要成为一种独特的情感记忆，是我们的一种心理需求。――如果还记得经常日本有当年“满蒙开拓团”的成员或后人会悄悄回到他们的“满洲”暗自凭吊一番，便会知晓这种人性的共通之处。无论是个人还是集体，对苦难生活和命运都会有相似的的耐受性。当年建州鞑子杀进家园，让无数男女被淹没在命运的血海中，最后，它还是变成了我们的大清。赵无眠的《假如日本战胜了中国》，愤青自不能接受，但道理却是如此冷酷无情。<br />
<br />
　　也许，拿日本殖民者来进行比附是严重的“政治不正确”，但在当年，他们响应国家的号召抵达各自的目的地时，情形又有何不同，今天的差别在于，一个是还在被肯定的有价值的“贡献”，一个则早已是“被欺骗”。但是的但是，“湘女回故乡”为什么在湖南这块地方这一活动会有如此的反响，的确还在许多人心中有别样的历史渊源意识，我母亲就说，当年她想到将来为孩子起名，首先想到的就是从杨昌浚的那首《恭诵左公西行甘棠》中取字。我不知道热烈响应者中是否有人还有那种潜意识，新疆之与湖南，用杨度《湖南少年歌》中肆无忌惮的说法便是：“茫茫回部几千里，十人九是湘人子。左公战胜祁连山，得此湖南殖民地。”<br />
<br />
　　是的，我终于小心把话题引到这儿，已经是一种可怕的政治冒犯，但是在纪念“八千湘女上天山”时，可曾有人想过一些维族人默默旁观时，心头掠过的是什么感受？这是我在听到这个选题时，第一时间的真实反应。因为我在那里出生长大，有当时的切身体验，在二十年后，民族问题越来越敏感微妙，民族主义情绪越来越膨胀的时候，我无法不在这个时候产生联想。<br />
<br />
　　是的，当年的湘女无疑是一批不幸者，她们接到了天堂发出的邀请函，却被带到地狱，但是，终归她们一直不缺乏组织给予的意义和说法，终归有故乡大张旗鼓的纪念活动，与她们命运相伴的，始终不乏热烈的主旋律资源。而那些维族人呢。当年，他们在路边驻着坎土蔓看着一车车的汉人从东面来，扎下根，生而育女，后来，“老城里无数的维族社区消失，再也没立起来，只看到汉人的社区长了腿似的一路扩张”，一位在北京读书的维族学生在ＢＢＳ上这样说道。没有人倾听他们的声音，官方宣传的民族优惠政策在现实中的走形和冷嘲式的事实，会要他们怎么想，这需要掉过头来站在他们立场上才看得清。<br />
<br />
　　强势的族群会在心理感受上较弱势民族族群迟钝许多。很多年后，我才能想起来，当年在建设兵团的子弟学校里，我是经常参与到殴打欺负少数民族学生的行列中的，他们来自周围的小村落，人数非常少，遇到挨打只能取不反抗姿态。我不知道当年的同学有多少人在成年后终于开始意识到，这种在我们看来，更像是不懂事的孩子间的正常打闹，会给那些维族同学一生留下怎样的心理印记。也许多数打人者早已忘记，但我相信，那些被打的少数学生不会忘记。甚至，他们曾努力“汉化” 的尝试会成为一种耻辱，我记得我们班两位维族同学有着异常奇怪的名字：革命、革命古丽，我不知道那两位今天早已成“羊冈子”的女同学是否依然保留着时代烙在她们的符号？她们今天怎样才能向自己的孩子解释这奇怪的名字？<br />
<br />
　　我看过ＢＢＳ上愤青大骂疆独时举例说，有维族同学对他们声讨日本人的罪行时不但表现冷漠，甚至面露嘲讽之色，我相信发生过这样的事实。如果他们竟然能与汉族同学产生强烈的对日情感共鸣，那才叫活见鬼了，情形只会相反。――我以为，仇日愤青应当最善于理解这种反应，如果他肯稍微的以己度人，肯花哪怕一丁点时间知道一些历史的话。问题是，偏狭的人在对待一切问题上都是偏狭的。而原本，一个在人口和现实处境上占据绝对优势的族群，的确应当对少数族群历史留下的敏感心理有所体谅。因为，他们无法发出声音，也更没有人倾听他们的声音。当年新疆第一任督军杨增新统治时期，杨是努力学会了维语的，并且要求汉族官吏必须学会维语，但在今天，可有汉族官员通维语的？虽然这种比较不适当，但军阀时代和日本人统治满洲时期，的确比今天做得更好。　　<br />
<br />
　　和ｔａｒａｓ说起过回族近代不幸历史造就的一种偏狭心理的问题，甚至ｔａｒａｓ自己也认识到自己身上经常显示出这一矛盾：能清醒地觉察、警惕身上可能保有那种历史集体记忆的偏狭心理，但却无法始终使自己在任何时候有一种豁达开朗。――这该是圣人的标准了吧？不能不说的是，在新疆，当年土著汉族因为有着与其他民族长期杂居生活的经验，他们更能理解、尊重不同民族之间的文化差异，而其后大规模流入的内地移民根本缺少与异族共同生活的经验，尽管有强力的政治环境迫使他们互相要捏合在一起，但是，内地移民的持续猛增，稀释掉了这种数代人积累起的沟通和理解。孩子们从成年人那里得来的关于汉族与少数民族生活文化差异的解释，有很多是严重妖魔化而且是极具侮辱性的。<br />
<br />
　　也许正是基于这种补偿心理，我会下意识地站在他们一边，有意思的是，有回一位网友一定要见面，居然请我在清真饭店吃饭，原来，他一直认为我是穆斯林。有我这样的穆斯林，在碰到某些争执时，效果会好过ｔａｒａｓ吧？前几天，他又因为某人在Ｂｌｏｇ上对穆／斯／林的不敬之词而动怒，看上去他更像是神经过敏的一方。是的，据说，每年都有新闻同行因为这方面的原因受到处分，但是，这种文化和宗教的官方保护姿态，丝毫没有减少这种隔膜，某种程度上反加深了这种隔阂。正好这边一位去过新疆几次的同事说起这个话题，他竟然也一直认为，禁食猪肉源自宗教图腾。这种隔阂，我不认为，化解的责任在人数较少，缺乏真正话语权的一方。仅仅因为被官方话语屏蔽掉的历史，就应当如此。许多愤青在陈说美帝的虚伪时，强调他们对印地安人的屠杀和剥夺，强调他们迟到几百年的道歉的无关痛痒和伪善，但人家毕竟承认了历史。<br />
<br />
　　１０月份从深圳回了趟老家，出了火车站，路过一位在屋檐下躲雨的维族年轻人的摊子前时，突然灵机一动，冲着他说了声：yahximusiz（你好）！他眼睛里立即跳出热情喜悦的火花，为我翻烤羊肉窜时，话格外多，汉语。其实，普通人时刻关心的是讨生活，民族意识、情绪，他有，可现实更是现实，所求不多，一句“yahximusiz”、“Rahmat”已足够要人惊喜，因为他们平时听到的，不是恐怖分子就是小偷。有一天你会和我一样认为，这不公平。</div>

							
							
							
							
