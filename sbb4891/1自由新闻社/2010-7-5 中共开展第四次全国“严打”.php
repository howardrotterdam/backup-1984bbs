													<h2>中国开展第四次严打 政权不稳到了临界点？</h2>
						
						
																											<div id="postmessage_723217" class="t_msgfont">中国警方在1983年第一次开展“严打”，此后在1996和2001年有两次。今年六月中国公安部要部署了第四次为期7个月的第四次“严打”。本台记者闻剑为此邀请在中国的黑龙江律师韦良h和中国天网人权事务中心主任蒲飞进行讨论<br />
<br />
记者：“因为严打涉及到是法律问题，我首先问一下韦律师，韦律师你认为这一次为期七个月的全国第四次严打能和过去不一样吗？”<br />
<br />
韦良h：“本身的‘严打’这个词可能是在国外都不会这么去做。正像有的学者所谈到的这种严打确实违背法治精神。应该说我们国家最根本的法律还是《宪法》，《宪法》也明确规定了中华人民共和国实行依法治国，建设社会主义法治国家。但是这种严打它往往是在法治、法律规定以外的，甚至可能突破法律规定的一个界限进行对犯罪的一种打击。从我们律师来看呢，无论是在任何时候作为司法行政机关包括公检法在执法过程当中都不应该离开法律的严格的这种规定的界限。但是往往在严打过程当中，特别是法律规定这个程序、标准掌握得不好。”<br />
<br />
记者：“那接你的话我再问一下蒲飞浦先生，观察1983年，1996年和 2001年的三次严打。就像有人所说的‘这三次严打都是运动式的严打，都摆脱不了一阵风的结局’。严打过后一段时间社会环境又会回到以前，并没有本质上得到改善，社会矛盾并没有得到多少缓解，老百姓的安全感也没有得到多少提高，因为这一次全国第四次严打的主要原因根据官方公布的消息是因为老百姓的安全感下降。这次严打的期限是七个月。那刚刚韦律师的评论这个严打期间会影响到法律应该的公正、公平、公开和它的恒久性。那你看这次全国第四次严打能与过去的三次严打不一样吗？”<br />
<br />
蒲飞：“从现在看到的迹象来看，它与过去的严打有些不一样的地方，而且历次运动式的严打它最后并没有对社会的和谐或者社会的稳定起到任何的帮助，反而促进了很多恶性案件的发生。如果我没记错的话，当年白宝山案件，白宝山最早就是在83年严打的时候被抓入监狱被判徒刑的，从此产生了对社会的报复心理，然后来犯下很有名的白宝山的系列枪案，就是枪杀军警的枪案。它这个严打，它当时有很多判决的严打的整个抓捕过程都是非法治的，甚至还是非理性的。在严打时曾经有人因为偷窥而被枪毙，这在全世界任何国家都不会存在这种现象。它这个严打还导致了大量的冤假错案，就是过度惩戒案件的发生，到最后会出现庞大的上访群体。在北京很多上访的人员岁数比较大的往往就是83年严打的受害者。而且它这个运动式的严打不以法治为准绳，而是以长官意志为准绳，这种严打它到底能维护法律还是维护长官的个人意志？这应该是结果很明确了。”<br />
<br />
记者：“但是有的专家分析这一次严打与过去的三次不一样，因为前三次都是由各地的党委牵头，公检法一起上阵，更多的是体现国家暴力实施的专政行为。针对的是一切治安犯罪案件贯穿着整个刑事司法活动。但这次不一样的地方是在‘是公安部一家的执法行动，是出于加强治安保卫工作的需要’。他们这种解释有没有道理呢，蒲先生？”<br />
<br />
蒲飞：“我认为他们这种解释有一定的道理，但是他们还是忽略了一些实际的情况。在很多地区公安局的局长就是当地的政法委书记，中国的司法体系是由政法委管理所有的司法机关，也就是说这个公安局长仍然可以在严打期间很正常地指挥检察院的检察长和法院的院长。这种政法体系的存在它不可能让这个严打变成只公安部的严打，它仍然还是一个政法系统的严打。”<br />
<br />
记者：“好，那我问一下韦律师，那韦律师你同意吗？”<br />
<br />
韦良h：“我认可他这个观点，而且在我们这个国家，公检法的这种相对独立性根本体现不出来。也就是说，作为公安机关侦查的案件在检察机关很难不起诉。那么作为检察机关起诉的案件法院很难不判决。所以说在法律形成这种互相制约的机制在实践当中是无法落实，无法实现的。从我这个角度来讲，我倒是认为这种严打很难甚至达不到作为严打最终确定的一个目的，因为从现在这个严打也好，在之前的几次严打也好，它可能最终的目的是想形成一种社会安定的环境。但是据我所知，在严打的期间老百姓也好，从我们这个法律研究人员的角度也好，感觉不到这种安全。严打本身，严打期间感觉不到这种安全；另外一个呢我认为解决社会矛盾的方法、方式不是通过严打这种方式能解决的。那么它需要一种社会的疏导、梳理，需要确立一种社会能得到社会公平的这么一种机制，我认为现在社会矛盾发生的根本的原因并不是我们国家的司法机关没有对真正的犯罪者依法进行惩治，而是因为现在这个社会的公平取得或者获得公平的渠道并不是很畅通。”<br />
<br />
记者：“好，那蒲飞你怎么看呢？”<br />
<br />
蒲飞：“从严打这个态势来看，我认为老百姓的不安全是一部分，从另外一部分来讲，他们也认为社会的整体稳定程度是不是到了一个临界点？是不是到了一个比方说危险系数较高的一个程度了？我个人认为老百姓的不安全感并不是来源于所谓社会上的犯罪分子，它是来源于这样一个权力不受制约的政府。老百姓对自己财产的安全感往往是由于城管队、拆迁办来搞出来的，他并不是黑社会的欺行霸市搞出来的，这是很少的一个现象。”<br />
<br />
以上是本台记者闻剑与中国黑龙江律师韦良h和中国天网人权事务中心主任蒲飞讨论中国正在进行的第四次严打。</div>

							
							
							
							
