&nbsp; &nbsp; 照我的理解，从严、从宽，都得在法律的限度度内。<br />
&nbsp; &nbsp; 为什么有从严、从宽之说？<br />
&nbsp; &nbsp; 那是法律的规定有一定的弹性，有一定的幅度。如从刑罚的种类上看，就有有期徒刑、无期徒刑、死刑；从刑期来看，有长的，也有短的。<br />
&nbsp; &nbsp; 一种罪，可能会涉及到多种处罚刑种，如故意杀人罪，就涉及到有期徒刑、无期徒刑、死刑；一种罪，也会波及到刑期的长短，从几年到十几年甚至无期等。<br />
&nbsp; &nbsp;所谓“从严”就是说按照法律的规定，处以最严重的刑罚。如故意杀人的，处以死刑，就是“从严”，而处以有期徒刑，就是“从轻”；一种罪，刑期有长短的，处以较长刑期的，就是“从严”。<br />
&nbsp; &nbsp; “从轻处理”，在刑法上有明确规定，这就好理解；而“从严”处理，在法律上没有明确规定，这就容易人人疑域。<br />
&nbsp; &nbsp; 在实际中，是从轻还是从严，这得根据当时的形势等条件来决定。是从轻还是从严，只要是法律规定的范围内，就不能说是违法。</div>

							
							
							
							
