																											<div id="postmessage_507429" class="t_msgfont">“感谢你爹你妈没问题，首先还是要感谢国家。”昨日上午，国家体育总局副局长、国际奥委会副主席于再清参加全国政协体育界别分组讨论，谈起有运动员夺冠后感谢父母，于再清说:“小孩儿有些心里话没有表述出来”，运动员得奖感言“说孝敬父母感谢父母都对，心里面也要有国家，要把国家放在前面，别光说父母就完了，这个要把它提出来。”他表示，要加强对运动员的德育。<br />
<br />
此前，周洋在冬奥会上夺冠后说“让我爸我妈生活得更好一点”，感动了很多人。<br />
<br />
昨晚10点半，南都两会“说吧”互动平台照例在微博发布今日征求网友意见题目，于再清的言论激起最热烈的反响，截至记者发稿时，微博转发过千次，评论 500多条。网友一边倒地支持周洋，于再清副局长的“先谢国家”说法遭强烈质疑。<br />
<br />
网友陈丹伶评论说:“你是冠军，祖国妈妈拥抱你。不是冠军，只有亲妈拥抱你。”<br />
要把国家放在前面<br />
<br />
冬奥会上，周洋勇夺女子短道速滑1500米金牌。比赛结束后，周洋说:“拿了金牌以后会改变很多，更有信心，也可以让我爸我妈生活得更好一点。”这个被认为充满人性的获奖感言感动了很多人。<br />
<br />
不过，周洋的言辞在国家体育总局副局长于再清看来还是有一点不妥当。于再清称运动员为“小孩儿”，昨日上午他参加全国政协体育界别小组讨论时谈到，要加强对运动员的励志教育、德育。<br />
<br />
“西方表达方式很好，但是小孩儿有些心里话没有表述出来。”于再清说，运动员个人成绩的取得与国家的培养和投入是分不开的。“体育人才能够获得冠军，国家投入多少，我们清楚，小孩儿也知道，教练都把他们当自己孩子。”于再清以短道速滑队主教练李琰为例，“她说训练结束了，小孩儿说:知道了，后妈。后妈有点半开玩笑的意思，李琰说后妈也是妈啊。”<br />
<br />
于再清说，运动员得奖感言“说孝敬父母感谢父母都对，心里面也要有国家，要把国家放在前面，别光说父母就完了，这个要把它提出来。”<br />
<br />
高健:运动员德育为先<br />
<br />
全国政协委员、国家体育总局体操运动管理中心主任高健紧接着于再清的话说:“社会主义体制下，所有人的成长都离不开国家，国家给你多大投入啊。”他痛心地说:“德育教育出现了很大漏洞。我去过几次台湾，很多地方挂着忠孝节义、礼义廉耻。不能因为台湾用了，我们就不用了，这是2000多年来中华民族的精髓。忠于国家、孝敬父母、长辈、老师，要有原则，都是很好的东西。”<br />
<br />
高健认为运动队出现的问题是社会缩影，“运动员没有感恩之心，拿了成绩算自己的，少拿一分钱都不行。”高健称，改变现状要“以人为本，德育为先”，要重视中华传统文化、核心价值观，“要靠于丹的力量，百家讲坛的力量来恢复”。<br />
