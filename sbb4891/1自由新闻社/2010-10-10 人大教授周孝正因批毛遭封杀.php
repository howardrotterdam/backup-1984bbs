<br />
本报讯 （记者郭少峰）昨天一条消息在微博上传播开来，消息称“中国人民大学社会与人口学院教授周孝正遭到校方停课”。但昨晚中国人民大学教务处有关负责人和周孝正本人均在电话里解释说，“没有停课这回事”。<br />
<br />
昨天在微博上一条消息传播开来。传播者还用文字注释说，“周孝正是人大四大名嘴之一，人称单口相声之王”，但“学校已经停了他的课”。此消息在网上引起了网友的愤慨。<br />
<br />
