贬毛泽东「三年自然灾害是地道人祸」<br />
人民大学教授遭封杀<br />
<br />
继早前北京市海淀区教师进修学校高级讲师袁腾飞在课堂上大胆演讲否定中共历史的内容，被当局封查后，北京中国人民大学教授周孝正又如法跟上，在其讲座上大胆抨击中共时政，贬低毛泽东，遭校方叫停，演讲被封杀。但其演讲视频在内地网站大热，被疯狂传看。<br />
<br />
这名出位教授是北京中国人民大学社会学教授周孝正（ 63岁）。据悉，因周的社会学讲座太出位，不但针砭时弊、揶揄党（中共）史，还批评毛泽东等领导人，大受学生欢迎同时，引起校方紧张。校方早前下令取消了周的社会学讲座，但周的授课未受影响。<br />
<br />
网民疯传讲座片段<br />
<br />
毛泽东出席 1949年在天安门广场举行的建政大典。<br />
<br />
本报昨找到被网民疯传的他讲座一段视频。在这段长达 90分钟视频中，周以诙谐而深刻语言，剖析当代中国社会现象，涉及官场腐败、三年自然灾害、中共历史上党内权斗、中共领导人私隐等；全场演讲一气呵成，他在台上绘声绘影，台下不时传来笑声掌声。<br />
<br />
周孝正讲到上世纪 50年代末至 60年代初「三年自然灾害」时，直接引用国家权威水文纪录指，那三年是中国百年来最风调雨顺的年头，将几千万人饿死归咎于自然灾害「是骗人」，「那是一场地道人祸，是全世界最大暴政」。<br />
<br />
讲到中国最受关注的医疗改革问题时，他直指中共高官占用中国医疗资源七成以上，更以亲身调查资料指，北京三甲医院高干病房中，「七老八十的等死的高干比比皆是」，特别是一些已跻身「党和国家领导人」的垂死者，他们每人每天要耗用医疗费高达 20多万元人民币，「这是全国九亿农民们，连想都不敢想的」。<br />
<br />
周孝正是继袁腾飞后，被当局封杀的第二位大胆抨击中共历史的学者；他在各大电视台任谈话嘉宾、专家评论员时，同样不乏出位言论。有消息指，他已被校方列入今年退休名单。本报昨未能联络他本人作响应。<br />
