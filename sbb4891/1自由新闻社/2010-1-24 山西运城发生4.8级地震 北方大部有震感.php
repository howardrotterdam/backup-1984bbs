																											<div id="postmessage_449025" class="t_msgfont">井水突然升温 莫非地震前兆？ 兴师动众排查 原来水泵老化！<br />
2010年01月07日07:20山西新闻网 字号：T|T <br />
<br />
　　本报讯（记者翟少颖）1月6日，翼城县一位姓孙的热心读者给本报打来电话，反映该县一村民院中的水井近日水温突然升高，达到36摄氏度左右，结合各地极端天气增多，当地一些群众产生疑问：寒冷季节里井水如此异常升温，是否是地震前兆？孙先生希望记者就此事咨询一下有关部门，并请本报曾多次报道过的地震预报“土专家”汾西县农民赵洪澎谈谈看法。<br />
<br />
　　记者首先就此事向省地震局监测预报处进行了咨询。该处一位庞姓工作人员表示，此前尚未接到基层地震部门关于此事的汇报，但根据记者转述的内容可初步判断，该井水温突然升高，系由水泵故障所致的可能性很大。该工作人员还表示，此前我省曾多次出现类似情况，事后查明原因，多与水泵有关。通常基层地震部门实地排查后，如结论与地震前兆无关则不做上报。<br />
<br />
　　记者随后与翼城县地震局取得联系，该局业务股一位工作人员告诉记者，出现水温异常升高的水井，位于该县南梁镇东尹村村民尹某院中。早在2009年12月27日，东尹村党支部书记曾给局里打电话反映此事。他们当即建议该村民将水泵撤离水井再做观察。然而时隔数日，却得知该井水温仍在持续升高。鉴于这种情况，1月6日上午，临汾市、翼城县两级地震部门工作人员联合当地水利、电力部门前往现场进行了一次排查。在现场，他们对这眼井的水位和水质进行了测量与分析，并对井中仍在使用的水泵进行了检查，最终得出结论：该井水位、水质均无异常变化，而使用七八年的水泵已明显老化，处于带病工作的状态，由此导致井水被持续加热。事后获悉，该村民之前并未依言将水泵撤离水井。该工作人员说，在将水泵撤离后，井水温度很快开始下降，至当天下午，已经降至27摄氏度。<br />
