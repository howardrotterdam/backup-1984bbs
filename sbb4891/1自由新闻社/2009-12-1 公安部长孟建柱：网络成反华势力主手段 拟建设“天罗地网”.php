													<h2>公安部长孟建柱：网络成反华势力主手段 拟建设“天罗地网”</h2>
						
						
																											<div id="postmessage_349946" class="t_msgfont">2009-12-01 11:04:45　来源: 中国新闻网(北京)<br />
<br />
新华网北京12月1日电 今日出版的《求是》刊载了国务委员、公安部部长孟建柱的文章《着力强化五个能力建设 全面提升维护稳定水平》，文章如下：<br />
<br />
着力强化五个能力建设　全面提升维护稳定水平<br />
<br />
孟建柱<br />
<br />
&nbsp; &nbsp; 胡锦涛总书记深刻指出，执政党的各方面建设都应该体现到提高党的执政能力、巩固党的执政地位、实现党的执政使命上来。公安机关在贯彻落实党的十七届四中全会精神的过程中，必须牢牢把握党的执政能力建设这条主线，立足公安机关的性质、特点和担负的职责、任务，进一步加强和改进自身建设，全面提升维护国家安全和社会稳定的能力和水平。 <br />
<br />
当前，我国经济社会发展呈现出一系列新的阶段性特征，社会稳定领域呈现出人民内部矛盾凸显、刑事犯罪高发、对敌斗争复杂的基本态势。<strong>互联网已成为反华势力对我进行渗透破坏、放大破坏能量的重要手段，给公安机关维护国家安全和社会稳定带来了新挑战；</strong>各种社会矛盾的关联性、复杂性、敏感性明显增强，给公安机关驾驭复杂局势带来了新考验；各类违法犯罪和社会治安问题大量增多，给公安机关社会管理带来了新课题；人民群众法律意识、权利意识明显增强，舆论监督、社会监督力度空前加大，给公安机关执法活动提出了新要求。与新形势新任务相比，公安工作自身还存在着不少亟待解决的突出问题和薄弱环节。当前和今后一个时期，要在坚持不懈地抓好“三基”工程建设的同时，进一步深化公安信息化建设、执法规范化建设、和谐警民关系建设“三项建设”，以着力强化五个能力为重点，全面提升维护国家安全和社会稳定的能力和水平，不断增强人民群众的安全感和满意度。<br />
<br />
&nbsp; &nbsp; 继承发扬党的优良作风　着力提高群众工作能力<br />
<br />
&nbsp; &nbsp; 群众工作是公安机关一项最基本的工作，群众工作能力是公安机关一项最基础、最核心的能力。在新的历史条件下，必须毫不动摇地把群众工作作为一项基本工作、把提高群众工作能力作为一项基本功来抓，着力夯实公安工作的群众根基。<br />
<br />
&nbsp; &nbsp; 强化宗旨意识，增进群众感情。要教育广大民警牢固树立群众观念，时刻牢记宗旨、执法为民，全心全意为人民谋利益，始终做到人民利益至上。要带着对人民群众的深厚感情深入群众、联系群众，了解群众疾苦，听取群众呼声，满足群众需求，努力以真心换真诚、以真心换真情、以真心换民心，永远做人民群众的贴心人。<br />
<br />
&nbsp; &nbsp; 立足自身职责，维护群众利益。要立足公安机关的职责任务，坚持从人民群众最期盼、最紧迫、最急需的事情做起，多做顺民意、解民忧、惠民生的小事、实事、好事，多推出符合实际、实实在在的便民利民措施，在一点一滴中践行“人民公安为人民”的庄严承诺，最大限度地取信于民。要正确处理破大案与破小案的关系，既要严厉打击欺压百姓、危害一方的黑恶势力犯罪和侵害群众生命安全的严重暴力犯罪，又要有效整治严重影响群众安全感的多发性侵财犯罪和突出治安乱点，既要集中精力破大案，又要坚持不懈地破小案，切实履行好保平安、保稳定的主要职责，全力维护人民群众的根本利益。<br />
<br />
&nbsp; &nbsp; 顺应时代要求，改进工作方法。要改进工作作风，深入基层、深入实际、深入群众，听民声、察民情、排民忧、解民难，着力回应群众的新期待，不断满足群众的新要求。要认真总结推广基层公安机关创造的“警民恳谈”、“春风行动”、“万警进万家”等实践经验，完善“大走访”爱民实践活动长效机制，把走访联系群众制度化、规范化。要拓宽社情民意反映渠道，把领导干部接访、走访和下访、回访等多种形式有机地结合起来，切实解决群众反映的切身利益问题，从源头上减少和化解矛盾。要善于运用互联网等新媒体，建立方便群众、畅通民意、沟通互动的网上服务新平台。要深化社区和农村警务战略，推动警务前移、警力下沉。要坚持专群结合、依靠群众，探索新形势下组织发动群众做好维护稳定工作的新思路、新途径。<br />
<br />
&nbsp; &nbsp; 坚持面向基层，树立正确导向。基层民警特别是派出所社区民警，要把懂群众心理、懂群众语言、懂沟通技巧，会化解矛盾、会调处纠纷、会主动服务、会宣传发动等“三懂四会”作为基本功，着力练就做群众工作的硬功夫，争做爱民实践的典范。要从政策、制度、机制上建立“人往基层走、钱往基层流、干部从基层出”的正确导向，激励广大民警扎根基层、融入群众，为民奉献、建功立业。要把选人用人的眼光投向基层，让那些吃苦的人吃香、实干的人实惠、有为的人有位，决不让那些老老实实做人、踏踏实实干事、兢兢业业工作的老实人吃亏。要进一步改进基层民警绩效考评办法，把群众满意度作为最根本的衡量标准。要最大限度地为基层减负减压，确保基层公安机关将主要精力、主要警力用在做好群众工作和基础工作上。要充分发挥典型的示范引导作用，及时选树一批长期扎根基层、深受群众拥戴的爱民实践模范，努力培育更多任长霞式的公安局长、“马天民”式的公安民警，建设和谐的警民关系。 <br />
<br />
大力加强执法规范化建设　着力提高执法能力<br />
<br />
&nbsp; &nbsp; 坚持严格、公正、文明执法，是公安工作的生命线。要实现严格、公正、文明执法，必须首先做到规范执法，把加强执法规范化建设、提高执法能力作为事关公安工作全局、事关公安机关能力建设的基础性工作来抓。<br />
<br />
转变执法理念，注重执法效果。衡量公安机关的执法能力、执法水平，最终要看能否有效地维护社会公平正义、促进社会和谐稳定。无论是刑事执法还是行政执法，无论是打击犯罪还是社会管理，都要以这一标准来检验、来衡量。在当前复杂的执法环境下，公安执法活动必须着眼于 “最大限度地增加和谐因素、最大限度地减少不和谐因素”的总要求，统筹考虑国际国内两个大局，统筹考虑网上网下两个因素，既要坚持严格、公正、文明执法，又要坚持理性、平和、规范执法，实现法律效果和社会效果的有机统一。特别是在全面开放、高度透明的执法环境下，必须更加注重树立理性、公正、文明、规范的执法理念，把这一理念根植于每一名民警头脑中，落实到每一名民警实际行动中，贯穿到每一项执法活动和每一个执法环节中。<br />
<br />
&nbsp; &nbsp; 改进执法方式，规范执法行为。要按照理性、公正、文明、规范执法的要求，改进执法方式，切实从言行举止、方式方法上规范和改进民警的执法行为，使民警尽可能地在执法活动中做到明之以法、晓之以理、动之以情，以法为据、以理服人、以情感人，真正融法、理、情于一体，使人民群众通过公安执法活动，既感受到法律的权威、尊严，又感受到公安机关的关爱、温暖，实现执法形式与执法目的的有机统一。无论遇到的情况多么复杂，都不冲动、不感情用事，不说过头的话、不做过激的事，防止因执法行为不规范损害公安机关的执法形象和执法公信力。<br />
<br />
&nbsp; &nbsp; 健全执法制度，规范执法流程。加强执法规范化建设，关键在于健全完善各项执法制度，对执法活动进行有效的规范和约束。要以公安部制定实施《公安机关执法细则》为契机，从基层民警日常执法活动最容易发生问题的环节入手，健全执法制度，细化执法标准，严密执法程序，规范执法环节，确保民警在各项执法活动中、各个执法环节上都有法可依、有章可循。要强化执法监督，大力推进网上执法流程管理，逐步形成执法信息网上录入、执法流程网上管理、执法活动网上监督、执法质量网上考核的执法办案新模式，实现对执法办案活动的全程化、实时化、动态化监督。要大力推行执法告知制度，最大限度地将执法依据、执法程序和执法结果公之于众，确保执法权力始终在阳光下规范运行。 <br />
<br />
深入推进信息化建设　着力提高实战能力<br />
<br />
&nbsp; &nbsp; 信息化对于整合警务资源、改造警务流程、创新警务模式、提升警务效能发挥了不可替代的作用，已经成为提高公安机关核心战斗力的重要途径。必须坚定不移地走科技强警之路，深入推进公安信息化建设，坚持以情报信息主导警务，着力提高公安机关的战斗力。<br />
<br />
整合信息资源，实现全警共享。信息化建设的基础在于信息系统和信息资源的整合共享。要整合各种系统资源，着力打破 “条块”壁垒，力争尽快建成纵向贯通、横向集成、优势互补、资源共享的公安信息平台。要强化基础信息采集录入这一源头性工作，把随时随地掌握情况信息、随时随地录入信息数据作为基层民警的重要职责和日常工作方式，作为牵动人口管理和防范控制等基础工作的纽带，做到底数清、情况明，管得了、控得住，着力实现基础工作信息化、信息工作基础化，为构建信息统一平台、实现信息共享应用提供强有力的支撑。要加快推进信息化深度应用，进一步加强对情报信息的综合分析研判，不断提高预知、预警、预防能力，牢牢掌握公安工作的主动权。<br />
<br />
&nbsp; &nbsp; 强化实战应用，提高打防能力。信息化建设的关键在于实战化应用。要充分依托信息网络，大力推行网上异地同步调查取证、侦查破案和网上追逃等打击犯罪新机制，着力降低打击成本，增强打击效益，提升打击水平。要积极适应动态环境下社会管理的要求，充分发挥信息化的关联优势，健全完善打防管控一体化运作机制，努力在防范管理中发现打击重点，在打击整治中发现防控漏洞，不断提高对动态社会的整体管控能力。要全面加强街面防控网、社区防控网、单位内部防控网、视频监控网、区域警务协作网和“虚拟社会”防控网等“六张网”建设，积极构建点线面结合、人防物防技防结合、打防管控结合、网上网下结合的社会治安防控网络，努力实现对动态社会的全方位、全天候、无缝隙、立体化覆盖。<br />
<br />
&nbsp; &nbsp; 推进警务创新，提升警务效能。在当前维护稳定任务繁重艰巨、警力不可能大幅度增长的情况下，必须坚持向信息化要警力、要效率、要战斗力，以信息化推动警务机制创新，着力提升警务工作效能。要通过信息化，大力推行扁平化指挥模式，最大限度地压缩指挥层级、减少中间环节，着力拉近指挥与实战、机关与一线的时空距离，缩短响应时间，提高快速反应能力；大力推行符合基层实战化要求的警务模式、勤务方式，科学配置警务资源，合理调整警务部署，最大限度地把警力投向社会面，进一步提高对社会面的管控能力；大力推行网上管理服务工作，进一步方便群众办事，提升管理服务的整体效能。<br />
<br />
妥善处置各类重大突发事件　着力提高驾驭复杂局势能力<br />
<br />
&nbsp; &nbsp; 积极预防、妥善处置重大群体性突发事件，不仅关系广大人民群众的切身利益，而且关系改革发展稳定大局，关系党和政府的公信力，是对我们驾驭复杂局势、提高执政能力的重大考验。<br />
<br />
坚持预防为主，及时化解矛盾。群体性事件大多与利益纠纷有关，预防群体性事件的根本立足点在于化解矛盾纠纷。多年的实践证明，矛盾在第一时间、第一环节解决成本最小，必须始终坚持预防为主、调解为先。“枫桥经验”的关键，就在于“小事不出村、大事不出镇、矛盾不上交”。要继承和发扬新时期的“枫桥经验”，把工作的着力点更多地放在基层、放在平时、放在准确掌握社情民意和及时把握社会动态上，深入开展矛盾纠纷排查调处工作，着力化解各类矛盾纠纷。对群众反映的切身利益问题和可能引发重大群体性事件的不稳定苗头，要及时报告党委、政府，积极依靠党委、政府，千方百计解决问题、缓解矛盾、化解纠纷，最大限度地把矛盾纠纷化解在基层、把问题解决在当地、把隐患消除在萌芽状态，从源头上预防群体性事件的发生。<br />
<br />
&nbsp; &nbsp; 迅速控制局势，尽快平息事态。处置群体性事件，必须做到发现要早、化解要快、处置妥当、防止蔓延，力争取得最佳的处置效果，把对社会稳定的影响和危害减小到最低限度。一旦发生重大群体性事件，要坚持早发现、早报告、早控制，努力确保事态不扩大、不升级、不激化。处置群体性事件，要十分讲究政策方法，特别是对群众反映的利益诉求，要及时报告党委、政府妥善予以解决，切实维护群众的合法权益；处置中，要多做宣传、解释、说服、疏导工作，着力缓解群众情绪，引导群众以理性合法的形式表达利益诉求、解决利益矛盾、维护自身权益。对插手群体性事件、蓄意制造事端的幕后组织者以及实施打砸抢烧的违法犯罪分子，要及时固定证据，依法坚决打击处理，切实维护法律的权威和尊严。<br />
<br />
&nbsp; &nbsp; 健全指挥机制，提高处置水平。各级公安机关领导同志要不断总结指挥处置重大突发事件的经验教训，切实加强对重大突发事件规律特点的研究，提高现场指挥、临机决断、依法处置的能力和水平。要健全统一指挥、反应灵敏、协调有序、运转高效的应急指挥机制，明确各部门、各警种的职责任务，着力提高应急指挥效能，形成处置重大突发事件的整体合力。要大力加强应急处突专门力量和装备建设，不断完善处置预案，强化实战演练，提高应急处突能力。 <br />
<br />
树立现代传播理念　着力提高与社会公众沟通能力<br />
<br />
&nbsp; &nbsp; 互联网等新媒体的兴起，使社会舆论环境发生了重大而深刻的变化，积极研判、回应网络舆情，提高与社会公众沟通的能力，已经成为加强公安机关能力建设的重要任务。必须把积极应用网络平台，加强与人民群众的信息沟通，正确引导网络舆情，放在更加突出的位置，努力拓宽信息化条件下加强和改进公安机关舆情引导工作的新路子。<br />
<br />
及时发布信息，公开事实真相。一起重大事件的处置工作，包含现场处置和舆情引导两个重要环节。要在及时、准确地做好事件、案件现场处置工作的同时，高度重视舆情工作，及时发布权威信息，公开说明事件真相，努力取得良好的法律效果和社会效果。要按照信息公开、依法行政的要求，深化警务公开，在不影响侦查破案的前提下，凡是可以向社会公开的信息都要及时公开，切实提高警务工作的透明度，满足群众的知情权，自觉接受社会监督。<br />
<br />
&nbsp; &nbsp; 完善工作机制，正确应对舆情。要建立健全重大涉警舆情快速反应机制和沟通协调机制，一旦发生重大突发事件，迅速启动应急处置机制，相关部门各司其职、各负其责。要建立健全舆情会商研判机制，坚持早发现、早应对、早引导，及时澄清事实，将有可能酿成重大舆情危机的不稳定舆情苗头化解在萌芽状态。流言止于公开，谣言止于智者。要进一步完善公安机关新闻发布制度，确保一旦发生重大涉警舆情，能够在第一时间客观公正地发布权威信息、公布真相、引导舆论，把握主动权。<br />
<br />
&nbsp; &nbsp; 注重公众需求，提高沟通能力。要充分发挥新闻媒体在公安机关和社会公众之间的桥梁和纽带作用，加强同媒体的联系沟通，主动向媒体介绍公安工作情况，尽可能地为其采访报道提供便利，满足其对公安新闻素材的需要，同时要自觉接受舆论监督，最大限度地争取媒体对公安工作的理解和支持，确保媒体客观报道、公正报道。要改进公安新闻宣传的内容和形式，善于把公安工作中生动鲜活的内容以群众喜闻乐见的形式传播于众，不断增强公安工作的影响力、感染力，努力为公安工作营造良好的舆论氛围。</div>

							
							
							
							
