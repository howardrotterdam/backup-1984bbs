																											<div id="postmessage_420944" class="t_msgfont">只会写自己名字的港大院士<br />
本报记者 赵涵漠文并摄<br />
中青在线－中国青年报　　　 2010-01-06 　<br />
<br />
&nbsp; &nbsp; 她没有上过大学，也不知道什么是“院士”。她一生只学会写5个字，却被香港大学授予“荣誉院士”。她没做什么惊天动地的伟业，只是44年如一日地为学生做饭、扫地。在颁奖台上，这位82岁的普通老太太被称作“以自己的生命影响大学堂仔的生命”，是“香港大学之宝”――<br />
<br />
<img src="http://zqb.cyol.com/images/2010-01/06/xin__EWM_180107060202375125378.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
<img src="http://zqb.cyol.com/images/2010-01/06/xin__EWM_330107060202484286139.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
&nbsp; &nbsp; 袁苏妹从没想过，在自己漫长的生命中，也有可能站在舞台中心。2009年9月22日,当香港大学向她颁发荣誉院士那一刻，这个82岁的老太太，“看起来神气极了”。<br />
<br />
&nbsp; &nbsp; 她被安排压轴出场。这一天与她同台领奖的，有汇丰银行曾经的行政总裁柯清辉、香港富豪李兆基的长子李家杰，以及曾获铜紫荆星章的资深大律师郭庆伟。<br />
<br />
&nbsp; &nbsp; 与这些政商名流相比，这位老人的履历显得异常单薄：学历，没读过小学，除了自己的姓名，她当时还不会写其他字；工作经历，从29岁到73岁，在香港大学的大学堂宿舍先后担任助理厨师和宿舍服务员等职。<br />
<br />
&nbsp; &nbsp; 这场历年完全以英语进行的典礼，此刻因她破天荒地使用了中文。香港大学学生事务长周伟立先用英语宣读了写给这位老人的赞辞，接着又以广东话再次致辞。直到此时，从未受过教育的袁苏妹才听懂，颁发院士的荣誉，是为了表彰她“对高等教育界作出独特的贡献，以自己的生命影响大学堂仔的生命”。<br />
<br />
&nbsp; &nbsp; 如果不是那一身黑绒红边的院士袍，她看上去实在是个再普通不过的老太太。她走路很慢，弓着背，一副典型的老态龙钟的模样。然而在港大人眼里，这个矮小的女人形象“高大”得近乎“一个传奇”。<br />
<br />
&nbsp; &nbsp; 有人开始称她为“我们的院士”，但她显然更喜欢另外一个称呼――“三嫂”。因为丈夫在兄弟中排行第三，三嫂这个称谓被港大人称呼了半个多世纪。<br />
<br />
&nbsp; &nbsp; “三嫂就像我们的妈妈一样。”很多宿舍旧生都会满怀深情地说出这句话。当然，就像描述自己母亲时总会出现的那种情况，这些年过半百、两鬓斑白的旧生，能回忆起的无非都是些琐碎的小事。<br />
<br />
&nbsp; &nbsp; 今年70岁的香港电视广播有限公司副行政主席梁乃鹏还记得当年考试前“半夜刨书”，三嫂会给他煲一罐莲子鸡汤补脑。已经毕业15年的律师陈向荣则想起，期末考试前夕高烧不退，三嫂用几个小时煎了一碗凉茶给他，“茶到病除”。<br />
<br />
&nbsp; &nbsp; 时常有学生专门跑到饭堂找她聊天。男孩子总会向她倾诉自己的苦闷，诸如不知道如何讨女友欢心之类。女孩子也会找到三嫂，抱怨男孩子 “只顾读书，对她不够好”。多数时候，三嫂只是耐心地听完故事，说一些再朴素不过的道理，“珍惜眼前人”，或是请他们喝瓶可乐，“将不开心的事忘掉”等等。每年毕业时分，都会有很多穿着学士袍的学生特意跑来与她合影留念。<br />
<br />
&nbsp; &nbsp; 就连大学堂球队的比赛结果，三嫂也常常是第一个知道消息的人。“输赢都好。”她乐呵呵地说。迎接球队的总是她最拿手的菜远牛河或马豆糕。<br />
<br />
&nbsp; &nbsp; 那些大学时独特的味道，成为旧生每年聚会时永恒的话题。一位40多岁的中年男人像个孩子一样夸耀三嫂的手艺：“你知道吗，大西米红豆沙里面的西米直径足有1厘米，好大一颗！”<br />
<br />
&nbsp; &nbsp; 很少有人知道，为了将这些“大西米”煮软，三嫂要在灶台前站上两个多小时。为了让红豆沙达到完美，她只在其中放新鲜的椰汁。而蒸马豆糕时，为了让它“有嚼劲”，她必须用慢火煲1小时，“不停地用汤勺搅拌”。<br />
<br />
&nbsp; &nbsp; 然而自从上世纪70年代安装心脏起搏器以来，三嫂再也无法继续在厨房工作了，这位已经是5个孩子的母亲从此转做清洁工。男生们历来喜欢在饭堂开派对，每每狂欢到凌晨两三点，尽管这早就过了三嫂的下班时间，但她总是等到派对结束，再独自进去清理地板上的啤酒、零食和污渍。<br />
<br />
&nbsp; &nbsp; 那个在凌晨的饭堂里独自拖地的驼背老人背影，让许多学生总“不敢忘记”。<br />
<br />
&nbsp; &nbsp; 直到今天，小女儿卫锦璧还记得妈妈“见学生比见家人的时间还多”。尽管竭尽全力工作，三嫂一家当时的生活仍然十分拮据。她因不舍得花钱坐巴士，有时竟会提着40只鸡从街市一路走回山上的大学堂。<br />
<br />
&nbsp; &nbsp; 虽然父母都在食堂工作，但卫锦璧兄妹自小很少沾光。有时，三嫂会带回家一包切三明治剩下的面包皮，全家人的晚饭就是用热糖水泡面包皮。偶尔，猪油拌白米饭也能当一顿晚餐。<br />
<br />
&nbsp; &nbsp; 不过，这些记忆却在老人头脑里慢慢开始模糊。经常，她能记起自己29岁那年进入大学堂工作的情景，却怎么也说不清自己哪一年退休。<br />
<br />
&nbsp; &nbsp; 眼下，她最害怕自己患上老年痴呆症，担心不能像现在一样，“记得每一个宿舍旧生的名字”。直到今天，每次受邀参加旧生聚会，就算有人已经移民十几年刚刚回港，她也能一下叫出那人的名字。<br />
<br />
&nbsp; &nbsp; “这个是大律师，这个是做生意的，这个是眼科医生。”翻开相簿，她就像介绍自己的孩子一样介绍这些学生。她甚至记得一名只在大学堂住过3个月的北京教授，对方曾邀请她一起“打太极拳”，她“怪不好意思地”拒绝了。<br />
<br />
&nbsp; &nbsp; 今年已经67岁的香港赛马会主席陈祖泽，在三嫂的眼里“好文静”，仍然是个“乖仔”。被授太平绅士的梁智鸿，大学时总是“穿着整齐才进食堂吃饭，从来不会穿拖鞋”，只不过“大学时头型就是中分，不知道为什么到今天都没变过”。<br />
<br />
&nbsp; &nbsp; 有男生甚至称，三嫂至今还记得他们大学时历任女友的名字。<br />
<br />
&nbsp; &nbsp; 总是开名车接送三嫂参加旧生聚会的陈向荣说：“能够做她的司机是我一生的荣幸。”<br />
<br />
&nbsp; &nbsp; 这些有关三嫂种种琐碎的“好”，事隔若干年仍然潜伏在旧生们的记忆中。三嫂却说不清自己究竟“好在哪里”。在她看来，“拎出个心来对人”，人生其实就这么简单。在宿舍工作时，她自己的大儿子正在美国读天文学专业，她只是用“母亲的心”去照顾这群同样在外读书的孩子。<br />
<br />
&nbsp; &nbsp; 如今她早已经退休了，但她其实从来没有真正离开过大学堂。<br />
<br />
&nbsp; &nbsp; 每年，她会回来制作“宿舍之血”。这种由生抽、老抽、番茄酱、豉汁、胡椒粉、辣椒酱制成的“饮料”，除了三嫂，“没有人调得出那么正宗的味道”。<br />
<br />
&nbsp; &nbsp; 大学堂的传统节目“拜齐天大圣”，也仍是由她来负责准备香火。<br />
<br />
&nbsp; &nbsp; 每遇迎新会或开放日，她总要在自家狭小的厨房，制作学生们最喜欢的马豆糕和大西米红豆沙，再由学生们带回去。<br />
<br />
&nbsp; &nbsp; 这位从没摸过教材的老人，压根儿不曾想到，自己会成为大学堂“迎新教材”的一部分。在名为“宿舍历史”的课程中，每年新生都要学习宿舍之歌：“大学堂有三宝，旋转铜梯、四不像雕塑和三嫂。”<br />
<br />
&nbsp; &nbsp; 更特别的荣誉出现在2009年6月。一天，三嫂突然收到香港大学校长徐立之寄给她的信，邀请其“接纳香港大学之名誉大学院士衔”。她事前毫不知情，此前，旧生会多次向学校提议给她颁发荣誉院士衔。直至2009年1月，周兆平副校长亲自作出提名。而在此之前，只有“社会名流”才有可能进入这份提名名单。<br />
<br />
&nbsp; &nbsp; “我相信三嫂是没有争议的。”这位提名者说。事实证明，三嫂的提名“全票通过”。<br />
<br />
&nbsp; &nbsp; 3个月后，“不知道院士是什么”的袁苏妹前去参加典礼，坐在面对600多人的台上，她一直试图记住前面的人“走哪条路、何时戴帽、怎样行礼”，生怕“忘记了整个程序”。<br />
<br />
&nbsp; &nbsp; 她坦承，直到走上台前，“袍子里面的腿一直在抖”。直到听到周伟立提到她曾经因为逃难而失去接受教育的机会时，她感到一阵“辛酸”，最后“糊里糊涂地”接受了副校监的颁授。<br />
<br />
&nbsp; &nbsp; 当时，观众中20多名“头发都白了”的旧生，兴奋地跳起来鼓掌喝彩，典礼负责人甚至不得不让工作人员走过去，请这些政商两界的知名校友“不要太激动，保持安静”。<br />
<br />
&nbsp; &nbsp; 次日，旧生们振臂簇拥中的三嫂被《苹果日报》头版以整版报道。此后，她登上了美食节目介绍自己拿手甜品“马豆糕”的做法，在娱乐节目“东张西望”中被专访，香港各大报章几乎都能找得到三嫂的照片。<br />
<br />
&nbsp; &nbsp; 授衔赞词中的一段也被各大媒体反复引用：“很多人知道，大学堂有三宝：铜梯、四不像和三嫂。三个宝贝搬不动，移不走，三嫂永远是大学堂之宝，亦是香港大学之宝。”<br />
<br />
&nbsp; &nbsp; 然而，这个爱看韩剧的老太太并没有被这些突如其来的荣誉打乱阵脚，尽管有时在街上，她偶尔会被陌生人惊喜地叫住：“你是三嫂吗？恭喜你。”<br />
<br />
&nbsp; &nbsp; “我的生活没什么变化。”三嫂一如往常淡淡地说。她仍然居住在北角区一幢建于12年前的公共屋屯卩,因为家里没有足够高的衣柜，她只能将红边黑底的院士袍和软呢院士帽叠起来，小心翼翼地收藏到盒子里。<br />
<br />
&nbsp; &nbsp; 大学堂旧生会为庆祝她荣获荣誉院士衔，特意在饭店里摆了30桌酒席，300多个旧生到场祝贺。而她能回赠的，只是一张张自制的、只有手掌般大小的卡片。由她口述、女儿打印的祝福文字，每一字都再普通不过：“读书口既，学业进步！”“做工口既，步步高升！”“做生意口既，生意兴隆！” “揸车、坐车口既，出入平安。”<br />
<br />
&nbsp; &nbsp; 只是，这个本来只会写自己名字的“院士”，足足用了两天时间又学写了两个新字。她一笔一画、签了300多张感谢卡――“三嫂”。</div>

							
							
							
							
