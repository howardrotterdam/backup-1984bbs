													<h2>北京持续高温 天气预报疑被操控</h2>
						
						
																											<div id="postmessage_723429" class="t_msgfont">北京连续三天气温超过36℃ 今日可能超37℃<br />
 2010年07月05日01:31&nbsp;&nbsp;新京报 <br />
　　本报讯 （记者林文龙）从7月2日开始，北京已经连续3天气温超过36℃，今明两天，北京气温仍将维持在35℃以上。昨天下午，北京市气象台继续发布高温黄色预警信号。北京市气象台称，目前北京气温高、空气湿度小，属于干热天气，但7日左右，将转变为高温高湿的闷热天气，8日开始有雨，气温将明显回落，届时北京才能暂别高温的困扰。<br />
<br />
　　今日气象条件有利于升温<br />
<br />
　　36.7℃、37.7℃、36.9℃，从7月2日开始，北京连续出现3个高温日（最高气温大于35℃）。北京市气象台一位专家介绍，到昨天为止，虽然北京连续出现了三天的高温天气，但真正的峰值还没有出现，有可能在今天，因为今天的气象条件比前几天更有利出现高温天气，气温也会高于昨天，明天的升温条件也不错。<br />
<br />
　　这次高温天气的特点是气温比较高，持续时间比较长，因此昨天16时10分，北京市气象台继续发布高温黄色预警信号称，受暖气团影响，今明两天，北京气温仍维持在35℃以上，今天城区及平原地区最高气温可能超过37℃。<br />
<br />
　　该时段高温天气常见<br />
<br />
　　“北京这个时候容易出现连续高温天气。”气象专家说，从历史数据看，在六七月份出现连续高温天气很常见，比如2007年，有连续7天的高温天气；2006年和2005年，有连续5天的高温天气；2000年，曾经达到过连续11天的高温天气。<br />
<br />
　　雨水将带走此轮高温<br />
<br />
　　一般来说，高温通常有两种情况，一种干热性高温；另一种是闷热性高温，称为“桑拿天儿”。这段时间北京的高温天气属于典型的干热性高温。气象专家称，今明两天，北京仍将持续这种干热天气。但到7日，空气湿度会增加，闷热感明显。气象专家表示，闷热天气里，人体不容易散热，可能会更难受。特别是夜间气温高，会影响大家休息，对生理和情绪都会产生影响。不过，8日开始，北京有雨，气温会有明显下降，届时，北京也将暂别高温天气。<br />
