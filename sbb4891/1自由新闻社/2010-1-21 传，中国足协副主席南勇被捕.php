第三次，“8&#8226;25”终于出手<br />
记者贾蕾仕北京报道&nbsp; &nbsp; 没有什么意外了，南勇、杨一民和张健强是被警方带走了，带走他们的，是沈阳的反赌专案组，虽然反赌风暴已经开始了几个月，但鲜为人知的是，沈阳的专案组真正的名字是“8&#8226;25”，更鲜为人知的是，这已经是“8&#8226;25”专案组的人，第三次来足协，此前两次，他们只是来调查，而并没有抓捕行动，足协官员范广鸣是在武汉直接被带走的，所以说直到第三次，“8&#8226;25”才动手，而且一下子带走两个足协大员。<br />
在沈阳“反赌”行动开始时，就有公安干警打电话到中国足协了解相关情况，并表示“会到足协了解情况，并拜访足协领导”。9月30日是专案组第一次到中国足协的时间，这是国庆节前的头一天，当时来了两名穿便衣的干警，到了足协后，两人了解情况做记录时，拿出了一个小本子，上面写着“8&#8226;25专案组”字样。<br />
当时足协工作人员还很好奇地问他们，为什么叫“8&#8226;25专案组”，干警说：“我们专案组是8月25日成立的。”足协才知道，沈阳警方在专案组成立时间，仅仅一个月后就到了北京足协总部调查，当时专案组还是在调查王鑫的案件，这应该是专案组后来调查的起点。<br />
两名干警重点是询问“调查王鑫，怎么才能找到他”以及“怎么才能了解相关情况”，在经过足协相关人员介绍后，双方还聊了起来，但是聊天内容没有离开业务，大多是在询问足球俱乐部和球队的基本运作情况，结束第一次接触后，两名公安干警没有透露任何与案情相关的内容，随后都离开北京回到沈阳。<br />
其后，在很长一段时间里，专案组没有在与足协联系，足协对案情的了解都局限于新闻媒体的报道，但是双方都有纪律，没有更多地接触。随着案情的被披露，专案组在11月10日第二次来到中国足协，这一次已经不局限于王鑫和范广鸣的案件了。<br />
11月10日“8&#8226;25专案组”只派了一名干警到足协，接待他的足协工作人员还记得他穿得很朴素，一点儿都不像一个警察，足协工作人员还开玩笑说：“他来的时候就像一个农民工。”而在过去的印象里，警察都是雄赳赳气昂昂的样子，但是他的打扮与这份威风相去甚远，玩笑归玩笑，这并不影响足协工作人员给了他耐心细致的情况反应。<br />
与第一次不同，这一次这名公安干警打开有“8&#8226;25专案组”字样的笔记本后，详细了解了广州医药、成都谢菲联等多家俱乐部的情况，这被认为是后来广药和成都两家俱乐部发生地震的根源。但是这名公安干警只是在做记录，丝毫没有泄露任何一点儿关于案情的情况，而且他问的问题过于专业，事后被认为“他像是个俱乐部老总，来这儿了解情况”，甚至连转会的情况都做了了解，这被猜测“是否会有人因为转会等问题被协助调查”。<br />
在这次协助调查中，公安干警甚至流露出会再针对任何足协工作人员的意思，这让足协误以为下一站只会是俱乐部，而没有想到第三次还是来足协“拉人”。在这名干警离开的时候，当时足协工作人员还很客气地说留下来，在餐厅一块儿吃点儿工作午餐，但是这名干警谢绝了，这显得很疏远，现在回想起来，或许当时“8&#8226;25专案组”就有了再“办”一个足协工作人员的消息。<br />
据悉，沈阳专案组在办案过程中，一直跟总局有联系，专案组每次来中国足协都是通过了总局，只是在把人带走之前，没有与任何一方通气，这是专案组办案的行事方式，这是他们在按公安部门规定的方式在做事，足协工作人员说：“第三次，他们终于来把人带走了”。<br />
 <br />
 <br />
林晓华挑起中超和国足<br />
记者贾蕾仕北京报道南勇、杨一民和张健强被警方带走后，今天上午11时，林晓华和薛立召集全体中层干部会议，会上对现在中国足协的业务进行了重新分配，总局和足协均确认南勇和杨一民短期内无法回归足协的事实，这意味着这一最震惊中国足坛的消息得到了官方的认可，这应该是目前中国足球历史上最震撼的事件了。<br />
就在19日下午，足协召开亚冠工作会议，召集了中超参加亚冠的四家俱乐部老总到北京，但是在足协的会议开始前，工作小组的正副组长南勇和杨一民都没有到会，林晓华的解释是“南勇和杨一民都要参加一个非常重要的会议”，当时四位俱乐部老总没有提出异议，这是足协领导无奈之下最后一次公开掩盖事实真相。<br />
20日上午，总局领导召集林晓华和薛立开会，表示“现在非常时期，要做好足协工作，稳定内部工作人员情绪”，但是没有向林晓华和薛立通报南勇和杨一民的情况。回到足协后，林晓华和薛立立即召开中层干部会，对接下来足协的工作进行了部署。<br />
