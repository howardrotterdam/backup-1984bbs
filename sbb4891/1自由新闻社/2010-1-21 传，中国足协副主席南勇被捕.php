南勇在足协期间经历了好几个部门主管的轮换，曾两度主抓过俱乐部，但是他曾在今年留下了给天津泰达俱乐部介绍阿里&#8226;汉的疑问，虽然天津泰达俱乐部方面曾表示过这是无中生有的事情，但是外界媒体还是猜测其中还是有一定的瓜葛。<br />
阿里&#8226;汉是南勇当初给国家队聘请的外教，当时的经纪人白川也是后来把爱福克斯介绍给中国足协的经纪，而阿里&#8226;汉去年到重庆力帆以及今年在天津泰达执教就自然会被认为是南勇的意见。当时被很多地方媒体曝光后，南勇没有出面澄清。甚至有些地方媒体也分析判断，由于运作外教如进入俱乐部会有一些佣金，这被认为是利益所在。这或许给南勇添了麻烦。<br />
 <br />
如何组织国家队组织比赛？<br />
举办国家队的国际比赛历来都是很麻烦的事情，需要当地足协和赞助公司共同运作，但是之前必须经过中国足协授权，赛事的主办者必须是“中国足协”，而且要通过足协到FIFA备案，由于足协举办比赛还没有一个完善的程序，这让足协确定比赛地给外界有不透明的感觉。<br />
过去盈方组织比赛时，大多放在广州，而且当时也会有很多地方单位提出了承办，但是被足协以场地、球迷等等种种理由拒绝，因此地方会员协会很多负责人也对足协提出异议，甚至有会员协会过激地表示“我们不塞钱就拿不到国家队比赛吗”。<br />
但是足协方面也表示过自己的公平，足协曾为中国队参加2010年世界杯预选赛进行招标，其中有之前做了中国国家队大量比赛的广州市以及附近城市，但是最终却花落天津和昆明，这让广州和当时的盈方方面都感觉不满，盈方认为“我们签了合同但是足协才是最终决定者，我们根本没有作用。”尽管当时主管领导是谢亚龙，可是南勇也曾具体负责相关业务，这或许会让他受牵连，不过他应该能够解释清楚。 <br />
 <br />
我的读后感 ：<br />
一、我认为人类进步的力量在于批判，这次足协两巨头被带走，符合我们的常识，有利于社会的进步，大鱼不抓，江湖不平。其它行业同理。<br />
二、刘晓新说当签版时看到这一幕幕新闻时，心里空荡荡的，我觉得这是一个好的知识份子应该的心理状态，正如我们共同写作《中国足球内幕》时的状态，恻隐之心，悲悯之情，所以不要说我们借搞垮中国足球之机达到个人目的，这本书，这份报纸，没有个人目的，只有行业诉求和文化追真，其实南杨也不是原罪，原罪是这个反智的管理机制，理解的人自然理解，不理解的人继续傻逼。<br />
三、 独自行走在北京寒冷的大街上，接到《南方周末》夏榆的短信，说采访我的稿子已成，另外《南都人物周刊》和《南方人物周刊》对我的人物专访接近尾声，不知是否可以正常刊登，我突然很想感谢新浪的陈彤、新周刊的封新城，足球报的谢弈和刘晓新和全体同仁，黄健翔、郝洪军、韩寒、易中天、术术、小党、齐海啸、谭飞、张小波、所有鹏友会成员……等等人，是他们，让我从一个足球记者成为一个自由的知识份子，从球评人转型到杂文写作者、小说家，两年被评为《新周刊》新锐力量，去年成为最佳年度博客，我很感恩。<br />
四、感恩我妈，我女人，不要哭了，就当过去十几天发生的一切，只是一场戏。向你们深鞠一躬，向坏人竖起中指。<br />
五、我想睡觉。此时，泪流满面。<br />
