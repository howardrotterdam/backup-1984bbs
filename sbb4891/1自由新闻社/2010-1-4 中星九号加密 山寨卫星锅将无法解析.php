<br />
<br />
　　记者调查发现，目前一些小区居民不看数字电视、安装山寨锅收看中星九号的节目。<br />
<br />
　　元旦过后，山寨卫星锅的用户很可能就收看不到其传输的48套免费电视节目，这一消息在网上成了讨论的焦点。<br />
<br />
　　天通苑居民康晓菲告诉记者，自从2008年6月中星九号上天后，周围就有不少邻居放弃数字电视，转投山寨卫星锅了。<br />
<br />
　　花上两三百元就能免费看到48套节目，比起每个月交18元有线电视费划算多了。但升级的消息传出后，让康女士十分焦急。<br />
<br />
　　而天通苑亚奥电子城卖山寨卫星锅的赵友福说，近一年来，单单他一家在东小口和天通苑一带就装了2500多套。<br />
<br />
　　但近日有不少用户打来电话询问今后怎么办。“电话都快爆了，现在只能等破解了。”赵先生对记者说。<br />
<br />
　　昨天上午，记者在中关村看到，挂着“山寨锅”招牌的商户非常少。在科贸大厦，记者仅发现两三家商户仍挂牌销售。<br />
<br />
　　老板田涛告诉记者，他周围的摊位几乎都销售山寨锅，中星九号要升级了，这些货暂时都用不了了。<br />
<br />
　　田涛称，现在一天也卖不出一两套，比起以前一天卖100多套时，生意淡多了。<br />
<br />
　　“都听说要升级，自然没人买了，就等破解后赚钱了。”田老板说，现在他们针对卫星加密的破解升级服务已经开始预订了，每升级一个锅估计能收个百八十元的。<br />
<br />
　　相关说法<br />
<br />
　　昨天，记者从广电总局获悉，中星九号直播卫星终端将通过卫星传输方式进行软件空中升级。<br />
<br />
　　升级后，山寨锅用户只能看到12套节目，直至完全黑屏。原有的“村村通”不会受影响。工作人员提醒市民，安装卫星接收器需要遵守法律规定，私自安装属违法行为，更不要轻信非法商家所谓的可破解升级山寨锅的说法。<br />
<br />
　　新闻链接：中星九号<br />
<br />
