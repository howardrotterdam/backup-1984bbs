当军服遇上Gucci.<br />
<br />
去年晋升少将级文职将军的宋祖英，3月3日作为全国政协委员第一次穿着海军新式军服参加两会。她那天没有化妆，素颜军装照片在网上获得极高点击。不过，这些流传很广的照片上都能看到她的军装，但看不到她手臂上挎的包包。<br />
<br />
昨天，天涯社区娱乐八卦论坛里，有网友发帖《军装宋祖英手挽Gucci包出席两会，时尚与朴实的交界》，照片里可以看到包包了，意大利著名奢华品牌Gucci的包包。<br />
<br />
宋祖英穿的是海军07式深藏青色常服军服，胸标、姓名牌、级别资历章、臂章、军衔袖章，胸前还挂着政协委员证，还有……手里的Gucci包。<br />
<br />
1、臂章<br />
<br />
2、胸标<br />
海军胸标，由银色铁锚、五星和海浪变形组成。<br />
<br />
3、姓名牌<br />
中文姓名为楷体，每字下标注相应的汉语拼音，材料为锌合金。<br />
<br />
4、级别资历章<br />
最上一排中间的是级别略章，其余为军龄略章。<br />
级别略章以底色区分，正职略章缀2枚、副职缀1枚五星星徽。图中的级别略章为土黄色底色和1枚星徽，标明宋祖英是副军级。<br />
军龄略章上有几条杠就代表几年军龄。图中有4个一年章，3个二年章，3个三年章，2个四年章，也就是说宋祖英的军龄是27年。<br />
级别资历章的排数也显示了军人的级别，图中5排表示军级。<br />
