开放大陆学生赴台求学的“陆生三法”19日下午在台湾立法机构三读修正通过，但仍设限“一限二不”。有关大陆学历认证部分，初步采认北京大学、清华大学等“985工程”学校及另3所学校，共计41校。<br />
“陆生三法”是指两岸人民关系条例、大学法、专科学校法，台湾要开放大陆学生赴台求学，就需要对上述三项法案进行修改。台湾立法机构通过“陆生三法”修正案，台湾将正式承认大陆学历，并开放大陆学生来台湾大专院校就学。<br />
修正后的“陆生三法”明确规定“一限二不”：限制承认医事学历、大陆学生不得报考“安全机密”相关系所、非台湾籍的学生不得参加公务员考试。民进党团提出的附带决议，明定陆生“应依两岸关系条例第10条第2项规定，不得从事与许可目的不符的活动”，不得于校内外从事专职或兼职工作，也获通过。<br />
台湾教育主管部门计划在9月公布实施相关办法，包括“大陆地区学生来台就学及停留办法草案”及“大陆地区学历检核及采认办法修正草案”。而有关大陆学历认证，初步采认北京大学、清华大学等“985工程”学校及另3所学校，共计41校，且以“不溯既往”方向规划。全台湾总招生名额总量限制在2000名左右，离岛则采项目申请模式，不受2000名额总量管制。<br />
根据台湾媒体报道，41所高校名单如下：<br />
北京大学，中国人民大学，清华大学，北京航空航天大学，北京理工大学，中国农业大学，北京师范大学，中央民族大学，南开大学，天津大学，大连理工大学，东北大学，吉林大学，哈尔滨工业大学，复旦大学，同济大学，上海交通大学，华东师范大学，南京大学，东南大学，浙江大学，中国科学技术大学，厦门大学，山东大学，中国海洋大学，武汉大学，华中科技大学，湖南大学，中南大学，中山大学，华南理工大学，四川大学，重庆大学，电子科技大学，西安交通大学，西北工业大学，西北农林科技大学，兰州大学，北京体育大学，中央音乐学院，中央美术学院。<br />
