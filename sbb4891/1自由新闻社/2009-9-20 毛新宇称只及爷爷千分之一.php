毛泽东孙子毛新宇称只及爷爷千分之一<br />
<a href="http://news.QQ.com" target="_blank">http://news.QQ.com</a>　 2009年09月20日01:13 　 重庆晚报　 　 我要评论(170) <br />
<br />
<br />
毛新宇全家到毛主席纪念堂献花<br />
<br />
点击浏览更多最新图片<br />
<br />
39岁的毛新宇是毛岸青与邵华之子，毛泽东唯一的孙子，现任军事科学院战争理论和战略研究部副部长，副军职。他在人民网的博客被评为该网2008年10大最受关注博客之首。获奖评语是，“毛泽东的嫡孙，全国政协委员，坚持用博客宣传毛泽东思想，获得网友赞扬。”<br />
<br />
毛新宇从5岁开始就背诵毛泽东诗词，他儿子毛东东两三岁的时候，奶奶邵华就教他唱《东方红》。毛新宇说：“他比我要聪明，那个时候就已经能背3首我爷爷的诗词了。”<br />
<br />
与记者一同下山的时候，天色已晚，一个游客没有认出他来，拍拍他的肩膀，问他大门口在哪里。他拿扇子向前一指，“直走，很快就到啦！”<br />
<br />
母亲引领我从事毛泽东思想研究<br />
<br />
尽管爷爷毛泽东去世时他只有6岁，但他仍然认为“爷爷是对我影响最大的人”，他写过不少歌颂爷爷的红歌，1995年还由中唱出过一张专辑。<br />
<br />
研究和宣传毛泽东思想是毛新宇为自己确立的毕生使命，而带他走上这条道路的恰是其母邵华。<br />
<br />
“我本以为大学毕业，我会继续研究我很感兴趣的明清历史，没想到我母亲坚决要求我去研究毛泽东思想。她当时说了一句话我到现在还记得，她说你如果不是毛泽东的孙子，你自己愿意做什么就可以做什么，但是你是毛泽东的孙子，你就有这个责任来研究毛泽东思想。”<br />
<br />
1993年，毛泽东诞辰100周年纪念日即将到来时，邵华很严肃地跟毛新宇谈话，鼓励他申请加入中国共产党。就在诞辰纪念日的前10天，毛新宇被批准入党。<br />
<br />
现在毛新宇是全国多所大学的客座教授，邵华非常支持他“走进大学宣传毛泽东思想”，甚至亲自为他制作过多媒体授课内容。<br />
<br />
伯父毛岸英被视为毛泽东对子女教育的成功典范。“我父母对我用最严格的教育，给我讲得最多的是什么？就是经常在我面前讲他们很缅怀我的伯父。在我成长的几十年里，不知道多少次听到伯父的故事。”为了让毛新宇记住伯父、学习伯父，在邵华的安排和带领下，毛新宇曾先后4次赴朝鲜为伯父毛岸英扫墓。<br />
<br />
爷爷批注的《二十四史》我是第一读者<br />
<br />
在毛新宇记忆中，大概从七八岁开始，他就在母亲带领下见了许多老一辈革命家，包括许世友、李德生、陈云、邓小平等。<br />
<br />
“行万里路”也是父母教育他的独特方式，“从8岁一直到上大学，这么多年，父母只要是假期有空，就带我去看中国所有爷爷战斗过的地方、所有革命老区。不仅看过革命圣地、革命老区，我还跟父母多次去过很多大型工厂和企业，包括边疆、农村。”<br />
<br />
一般人家里是“严父慈母”，毛新宇家中更像是“慈父严母”。邵华对儿子的教育抓得非常严，“我很小的时候妈妈就要求我背诵毛主席诗词，如果背不下来，妈妈就会生气，有时甚至不让我吃饭。”<br />
<br />
为了让毛新宇接上他爷爷的精神血脉，邵华请王震给中央办公厅写了一个批示。“让我去爷爷的书房看毛主席批的《二十四史》和哲学笔记，那时候我上大学。”<br />
<br />
1990年，他花了四五个月的时间，到中南海菊香书屋学习。“应该说，毛泽东批注的《二十四史》，我是第一个读者。”<br />
<br />
全国政协会上发光200张东东的照片<br />
<br />
毛新宇与妻子刘滨育有一子一女。儿子毛东东出生于2003年12月26日，与毛泽东同一天生日，女儿毛甜懿，出生于2008年8月28日。<br />
<br />
与多数名人对家人的低调保护不同，毛新宇丝毫不回避公众对家人子女的打量，他觉得这是毛主席的后代必须面对的。<br />
<br />
2008年全国政协会上，不少代表委员及媒体记者得到毛新宇送的毛东东照片。<br />
<br />
“我跟我爱人说，你看我开全国政协会，这么多委员、这么多记者都关注毛主席的第4代，我们有责任让大家知道东东怎么成长。所以我跟我爱人在政协会期间，拿了200张东东的相片送给关心他的委员、记者。到会上一下子就发没了，我特别感动。”《南方人物周刊》<br />
<br />
我只能比得上爷爷的千分之一<br />
<br />
――对话毛新宇<br />
<br />
记者（以下简称“记”）：你出门会不会常常被人认出来？<br />
<br />
毛（以下简称“毛”）：经常被人认出来。我父母本人也好，对我的教育也好，都是说，不要怕被人认出来，不要怕到人民中间去。毛主席在群众中威望很高，群众对他的后人有着朴素的情感。认出我们来，要求合个影什么的，我们都满足。<br />
<br />
记：对于祖父，你有怎样的感情？对他的认识和情感的变化经历了怎样的过程？<br />
<br />
毛：说句真话，我这辈子有3个没有想到。我本以为大学毕业，我会继续研究我很感兴趣的明清历史，没想到我母亲坚决要求我去研究毛泽东思想。现在看来，这个决策完全正确；第二，入党我也没有思想准备。我入党的日期特别有纪念意义，是1993年12月16日，10天后就是我爷爷诞辰100周年；第三，我没有想到我会参军入伍，更没有想到自己会当将军。<br />
<br />
我真正读懂毛泽东是在我当兵之后。如果我不参军不入党，我面对我爷爷的时候可能会更轻松，就像普通人家孙子面对爷爷一样。现在不行，从家庭角度讲爷爷是我的爷爷，但是从军人的角度来说，我要把我的爷爷当作我们的领袖、我们的统帅。<br />
<br />
不管我是去毛主席纪念堂，还是去香山双清别墅，以及其他我爷爷生前战斗过的地方，我心里都会这样想：爷爷看着我呢！我都做了些什么事，有些什么成绩，我怎么去面对老人家，怎么跟他老人家交代。<br />
<br />
记：关于红色伟人的解读流派不一，你本人持何种立场？面对后人对祖父的功过评说，你心中是否一直平静超然？<br />
<br />
毛：我很高兴爷爷的思想有了发展，有了邓小平理论和“三个代表”重要理论。但是我想，如果爷爷还活着，对中国的社会主义建设和工业化道路，他也会与时俱进，也会发展他的思想。爷爷作为一位历史人物，再伟大也是人，也会有或多或少的失误，我也能理解别人说他有一些失误。但从我内心来说，我爷爷这个人他所取得的成就和他的成功仍然非常巨大，这改变不了。<br />
<br />
记：你说过希望自己能够学成爷爷的知识结构，你觉得自己现在达到了他几成功力？<br />
<br />
毛：（连忙摆手）要说我和爷爷相比，我只是他的千分之一。他去世前什么状况你知道吗？我小的时候以为他跟大多数老年人一样，病痛啊什么的很痛苦。但是后来我爷爷身边的人披露，他直到逝世前一分钟仍然在读书，这一点真是太伟大了。他不是一般人！一般人早躺床上不动了。<br />
