													<h2>冉云飞: 伤心岂独谭作人？――谭作人妻女文章选录</h2>
						
						
																											<div id="postmessage_204155" class="t_msgfont"><a href="http://www.bullogger.com/blogs/ranyunfei/archives/314190.aspx" target="_blank">http://www.bullogger.com/blogs/ranyunfei/archives/314190.aspx</a><br />
