 三道严格而善变的门检终于通过，就连律师的助手都没让进。我们进了庭。30个座位的5号庭，放眼一看全不认识，很正常，30个人中，除了我们2个而外，都是被“安排”进来的。随后法官进入：起立。爸爸进来了，半年没见，笑着挥手打了招呼。该来的都来后，很多人关注的审理开始了。<br />
<br />
辩方律师是著名的浦志强和夏霖。浦志强曾是南方都市报“喻华锋案”的辩护人，前不久代理段磊案；夏霖律师曾是崔英杰的辩护人，今年邓玉娇案，他跟另一位律师的努力也有目共睹。<br />
<br />
我因为身份问题，见证了各方人士很想目睹却未能进庭的审判，同时也见证了某种专制和黑暗。浦志强律师身高体壮，一副北方汉子的硬朗，声音浑厚有力，陈词娓娓道来，力度十足。两位律师做了很多功课，准备了很多资料证据。但是，随着女法官无数次打断和阻止辩方律师说话，浦志强律师从激动升级到了愤慨。 “为什么不让我说话！辩护人的权利在哪里？！”权力机构很聪明，故意安排女法官，让两位大律师男人不便发飙。看得出女法官也很紧张。尽管律师陈述的都是有力证据，法官还是不管三七二十一地阻止，“我觉得与本案无关，请说下一条。”无语。。。不过法官也只是按指令办事，她的背后是某政权在说话。范晓，艾未未，艾南山三大证也被阻止了，不准出席，好歹也是三个有名有实之人。被定性为“敌对份子”的“WD”愿回国出庭作证也未被批准。律师要求播放的视频证据也不让放，理由竟是“与本案无关”，“节约资源，时间”之类。如此生硬的理由，着实让人愤慨，一个封建社会的专制法庭。最后，让爸发言，爸说他有一个书面稿，但是因为事件复杂，需要花很多时间陈述，“铁面无私”的女法官立刻打断，“好了，那你就把书面陈词交给我们就行了，你就自己简单讲一下。”其实那些都不难想象，一切都是走过场和形式而已。&nbsp; &nbsp;&nbsp; &nbsp;<br />
<br />
休庭后，两位大律师爆发了，夏霖向女法官说“现在休庭了，我可以说话了吧！你们真的太过分了！”浦志强说“我觉得 这次是个耻辱，也是你们四川法庭的耻辱！”说完后过来和我们拥抱，高大的汉子竟然哭了，我们一起拥抱着，我轻轻地拍着他，搞不懂究竟是他在安慰我们，还是我们在安慰他。他的哭，在我意识里，是出于在强权专制下的愤慨，毕竟当律师这么多年，从未遇到过这样的压迫。&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;<br />
<br />
夏律师也遭多次打断。发表完辩护词，最后说：四川是人文荟萃之地，多出英雄豪杰。并奉送武侯祠长联：“能攻心，则反侧自消，自古知兵非好战。不审视，即宽严皆误，后来治蜀要深思。”表示相信“四川方面有充分的政治智慧处理好这个案子”。&nbsp;&nbsp;<br />
<br />
我们收拾完律师的东西，还没走到门口，就听到门外轰鸣的掌声，迎接我们的是挤满几层楼的人们。未料到场面如此壮观，以致当时我还没反应过来，处在比较“旷”的状态。震耳欲聋的欢呼声和掌声，就像我们是英雄般。“你爸爸很伟大，你应该为他感到骄傲。”陌生人对我说着。人群里很多人在哭，一些陌生人过来跟我拥抱，他们如此的激动和热忱。那些人群中，有的我认识，大多不认识，是爸的朋友们。还有的来自北川等灾区，没有组织，都是知道消息后，自发赶来的，这点蛮让人感动。出去后，很多摄像机之类对着我们，也许出自哪一方的都有，有的摄像机被便衣抢下，激进一点的被关进车里带走。我出奇的淡定，一个阿姨说“你倒是很稳重。”其实我本身就一点不怕，只是在刚出5号庭时，那种如此壮观和感染人的氛围下，是想哭的，只是忍住了，从来不喜欢在公众地方流泪。人是种很容易被氛围所感染的动物。&nbsp;&nbsp;<br />
<br />
早已料到此次开庭不会宣判结果，结果也难以猜测。已经休庭许久，人们还是不愿离去，此时，人群中很多便衣等出动了，“已经休庭了，快回去！”我们和两位律师合影后，一起去吃饭，只有我注意到了，刚进去就有一桌男人一直看着我们，我知道是之前的便衣或者公安们，坐下后跟肖雪慧阿姨说了。席间，浦志强律师的电话一直响个不停，是来自各方的媒体，浦律师很黑色幽默，接起来问的第一句话就是“请问你们是不是海外敌对媒体？不然我也要跟谭先生一样被定罪了。” 浦律师给艾未未打了电话才知道，艾未未被管制起来了，还挨了打，不让出庭，居然连饭也没让吃。直到我们吃完饭正准备离开，艾未未才穿着印有爸头像的白T 血，带着人进来了。其实不只是艾未未，很多人当天都被管制了。看来这次里里外外的下了不少功夫，出动了不少人。那顿饭，居然让旁桌的陌生人提前埋了单，询问才得知是来自灾区的感恩人。高档酒楼与穿着朴素的灾区人们相应成彰。<br />
<br />
在现代社会里，如此公开的专制和打压，真有点前所未见。<br />
<br />
此文，仅以简短的见证与记录，8.12我所亲身经历的场面：那些政权的黑暗，追求正义的勇敢；封建的专制法庭与人性中闪闪发光的感动。&nbsp;&nbsp;<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; 2009.8.12<br />
<br />
 <br />
<strong>王庆华：我想说几句话</strong><br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;今阅大泽兄的打油诗“汨罗江上沉屈原，风波亭中斩岳飞，三千年来忠魂苦，最苦成都谭作人。”感慨万分。<br />
<br />
&nbsp; &nbsp; 二十多年了，谭作人对国家的大忠远远多于对小家的小孝。他数次哀叹：太对不起家人了！我劝慰他：自古忠孝难两全，大忠即为大孝。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;谭作人入狱后，我们不满十六岁的小女儿五月十二日曾去信写到：“爸：五月十五快到了，提前祝你生日快乐，并给你讲一个儿时听过的童话故事。在一个国家，有一个国王和三个公主。一天国王问三个公主：‘你们有多爱我？’大公主说：‘我象爱蜜糖一样爱您。’二公主说：‘我象爱花朵一样爱您。’国王听后十分满意。问到三公主时，三公主说：‘我象爱盐一样爱您。’国王一听勃然大怒，立刻下令将三公主关进牢房。故事讲到这里，我不禁一阵悲哀。不知三公主被她最爱的父亲关进牢里，心中是怎样的凄凉？明明如此爱着父亲，只是爱的方式不同，不象其他公主那样甜言蜜语，就被下了大牢。三公主何罪之有？其实这个故事有个相当好的结局。一个邻国的国王听说了此事，便向三公主的父亲发出邀请参加一个盛典，席间很多佳肴。一尝，味如嚼蜡。这时有人告诉他：菜里都没有放盐！国王恍然大悟，原来渺小的盐不甜不香，却是最必不可少的一部分。国王马上回宫下令释放三公主。从此以后，三公主得到国王最多的宠爱。这是有名的童话《盐之公主》。故事有个好结局，但现实生活中的结局令人叹息。国王能良心发现吗？悲哀！你的家人和朋友、猫猫狗狗、花花草草依旧思念你，愿大家的关心能如清风，在炎夏给你带来一丝清凉。你的女儿。”<br />
<br />
作人十六日回信：<br />
<br />
“我的女儿：此间规则为逢十发信，但我等不及了。面对你送上的生日礼物――一封充满大爱的家信和100元钱，我失眠了。我无数次披衣而起，仰望铁条分割的夜，想念同城异地的你！此刻的我，再一次明白了一个道理：有的‘近’，其实很远；有的‘远’，实在太近！我们分别已经50天整，但我却时时在在地感觉到，我每一天都能看见你……一代人有一代人的价值，一个人有一个人的命运。我们这一代人，是集体主义的产物。这是宿命也是一种使命――因为集体。有集体的或者公共的价值需要传递下去。有人选择了这件工作，使命就可能成为史命――一种历史的创造和命定。这并不等于说个体的价值不重要。在二元乃至多元的文明中，集体和个体各自存在，相安无事，共生共存。在我为人人向人人为我的转型期中，需要两个以上的声音。当三公主的盐成为稀缺资源的时候，我们这一代人，应该有人惭愧。因此，三公主选择了看见和说出，他不会感到委屈，请你放心。心，就是为了给予，伴随着一滴眼泪（泰戈尔）。这支歌曲，是大家的。这滴眼泪，是自己的――或者说，是自己和自己的家人共有的。我可以毫无愧色地说：我对得起这个社会。但我对不起你们――我最亲最爱的人。你们，就是我的眼泪。生命的量变是一天天减少，生命的质量是一天天提高。而生命升华成为精彩，必有一种大爱在其中，从心底发散出来。即使是昙花一现和浮光掠影，梦幻似地存在，它也使你充满光明，不再惧怕得失与生死。这是一种大自由的境界。现在想来有些歉然，又有些释然――我们之间从来没有刻意探讨或交流过什么，但你竟然能够理解这一切。你的理解，让我十分感动。你的理解，已经超越了‘小爱’。……但愿这一次‘灾难’能使你也转危为机，产生一个大的飞跃！……想念你的爸爸。”<br />
<br />
直至今日我仍然坚信，他那么爱的国家无论如何不会判他“颠覆”之罪！一个强大的国家真的容不得正直的人说几句真话？请不要让孩子们生活在困惑中，更不要让他们失望！谭作人们，该苦到头了！&nbsp; &nbsp;&nbsp; &nbsp;<br />
<br />
 2009、8、6</div>

							
							
							
							
