<br />
冉按：昨天王姐给我发来这三篇文章，我看后给王姐说，可否在我博客上发表，王姐只两个字：“当然”。在作人兄进去之前，我没见过王姐，但作人兄进去后，我见过王姐数次，其坚定有条不紊地处理诸种事务，真令人感动。我当然知道她一个人带着两个女儿（一个尚未成年）的艰辛，别人再多的帮助，都无法抵挡她夜深人静的时候，独自咀嚼作人进去后，她的苦涩与艰难。我知道她很感动于很多朋友们的努力与安慰，但别人再多的安慰作用究竟有限，虽然只是暂时失去丈夫，但这种深深的悲苦，也非个中人所能体会。<br />
 <br />
作人进监狱，另外遭受压力和打击的就是他两个女儿了。两个女儿，可惜我都没有见过，因为我未去过他家。本来12日庭审有与他家人（包括他大哥）见面的机会，也被公安对我的软禁给破坏了。这里三篇文章，有两篇文章涉及两位女儿，其中大女儿的“见证”相当有条理，理性且富有逻辑，还有一定的文采，真有乃父之风。不满十六岁的小女儿所写的信，通过非常准确的童话寓言，来表达对爸爸的坚定支持，可谓冰雪聪明。作人兄的回信有爱有公义，可谓文理兼胜。但最感动的却是：“你们，是我的眼泪”。在我看来，这是2009年中国良心犯给家人最深的爱意表达，真可以令木石动容。而这句简单而深情的话，在我看来，有流传千古的力量。<br />
 <br />
8月12日的庭审，检方和法官的枉法无理，在这三篇文章中的头两篇已昭然若揭，以后我们会进一会揭露法官、检方的丑行。但令人感动的是，那么多参加谭作人案庭审的民众，虽然没有机会旁听，但这样的民意本身就是比枉法的庭审更有力量的审判。我们作为一位公民，应该在这种时候，让谭作人的家属感到力量与爱，感到他们并不孤独，他们身后站着更多支持谭作人的人。谭作人不仅令他们家人引以为豪，也是追求正义的人们共同的楷模与骄傲。有许多人都说，8月12日的庭审，开创了四九年以来庭审的纪录，即许多非利益相关者对谭案的深切关注，民众正在觉醒而且不吝表达。这恐怕使许多法警、便衣、国保都大出意料，同时也让那些追求正义之士深感安慰，两相对比，其意义不言自明。<br />
 <br />
三篇文章均系全文转载，只是纠正了一些笔误、衍漏，余一仍其旧。千古艰难唯正义，伤心岂独谭作人？请大家继续关注谭作人案的后续诸多情况，请大家关爱谭作人兄的家属，我的感谢之忱难以言宣。2009年8月15日抗战胜利64周年9：24分于成都<br />
 <br />
 <br />
&nbsp; &nbsp;&nbsp; &nbsp;王庆华：作人，你还在期待公正吗？――09．8．12庭审后感<br />
 <br />
开庭前两天我去市法院咨询是否会公开审理，得到非常肯定的答复，所以，任何媒体关于此次能否公平、公开、公正审理，要求我谈看法，我的回答都同样是肯定的，起码公开是一定的了。公开可以让不明真相的人普遍受到教育，谭作人那几件事也该让公众清楚。该关该放，既然已进入司法程序，则应依法办事，“我不回避法律责任，也不承担不应负之责任，只为司法公正和社会正义！”谭作人如是说。<br />
 <br />
想必二十一世纪如日中天的中华人民共和国，审理一宗反革命案没有道理不公开。<br />
 <br />
&nbsp; &nbsp;&nbsp;&nbsp;起诉书太空洞、抽象，字里行间透出诸多不严肃，我期盼庭上能有不为人知的钢鞭抛出。（这是基于第一次律师欲见谭作人，办案人不准许称涉及国家机密），起码得与“颠覆罪”对应。谭作人的事早已不仅仅属于我们的家事，怎样判定孰是孰非关乎政府的水平，亦检验社会是想进步或者是相反。所以我渴望，我万分渴望成都政府能体现一贯的大气、和谐、包容，我们的政权固若金汤，我们能战胜天灾人祸。成都是文明之城，文化之城，政府定不会辱没自己的形象，也决不可以低估热爱成都的谭作人们的智商。<br />
&nbsp; &nbsp; <br />
但在进场的那一刻，只有我和我的大女儿拿到了旁听证，名额从两天前的不限到当日早上8时许电话中答应的5人，再到因名额有限，只能进3人，最后用果断的声音宣布，“我说2个就2个！”公开这个词，从字典里飞快地被删除。<br />
&nbsp; &nbsp;&nbsp; &nbsp;<br />
庭审从9：40到12：30，我看到和听到的是整个一场公诉方的走秀，他们拿不出我期待的钢鞭。谭作人的回答被审判长数次打断、提醒，只能回答“是”与“不是”，辩方的证人被告知经审查研究，与本案无关，不准予出庭（其中证人之一的艾未未被打和被控制已是事实）。我看到控辩双方头天傍晚才交换的证据，已被公诉人断章取义、弄得支离破碎，要求辩方律师回答。而辩方律师，每答必被审判长用“不必详细说明，只谈主要意思。”“你如果是。。。。。这个意思，我已明白，按公诉人今天规定的页码”，“我已听明白，下一个问题”。辩方律师忍无可忍，当庭抗议，“有看法下来再交换。”旁边还有定时的帮腔“遵守法庭纪律。”“听从审判长指挥！”。我明白了，但凡辩方律师开口，头顶上定会飘来录音机的反复播放。<br />
&nbsp; &nbsp; <br />
整场都是这样，近3个小时。<br />
&nbsp; &nbsp; <br />
玩笑开大了，基本上是国际玩笑！<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
我问我的邻座：“你了解这个案子不？”他马上浑身僵直，呆若木鸡，我不甘再问：“哎，问你。。。”他仍然当铜像。我基本明白了周围的角色。我不再看谭作人的背影，只专注地注视女检察官和她旁边那个会说话的木偶的嘴。因为我已清楚，只要辩护律师一说话，台上那几位穿着行头的道具就会发出早已编好的台词。<br />
&nbsp; &nbsp;<br />
接下来还会有奇迹？还会出现公平吗？<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
已近12点，但是还有3个程序未完：一：公诉人最后陈述――他居然有本事把起诉书的原版浓缩后，再次抛出来――对辩方的提问、证据的质疑不作任何回应。二：被告人最后陈述。在开庭不久，审判长曾允诺谭作人可在最后作书面陈述，然而，瞬间已变为将陈述书上交，他只能口头简短陈述。作人问：“可给多少时间。？”答：“两分钟。”作人请示：“我最多3分钟就可念完。”“你口头陈述。”法警将其陈述稿夺走。他的口头陈述只说了一句话：“我不认同起诉书上的罪名。不管主观的故意或客观的行为，都没有颠覆……”已被打断，从高台上唐突的飘来一个声音：“辩护律师做最后陈述！”两位律师同时愤怒了!“抗议！让人把话说完！他有这个权利！”作人也说：“答应我做书面陈述的！”录音机又播放了一遍：“请辩护律师做最后陈述！”律师再次抗议，录音机再次响起。无奈，夏霖律师为不被打断，用简短的、清晰的、漂亮的发言结了尾。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
没有丝毫的公平。<br />
&nbsp; &nbsp;&nbsp; &nbsp; <br />
公诉人在没有听到任何辩论的情形下，法官宣布辩论结束。没有开始，就结束了。<br />
&nbsp; &nbsp;&nbsp; &nbsp; <br />
法庭辩论本是控辩双方的事，但法官、检察官在与辩方律师的质问、抗议和服从中，磕磕绊绊地纠缠了三分之一的时间。执法者不是不恋战，是根本不战。用不战的强权手段，可定一个人的罪？法官们要急着“下班”，或者有人指令他们快快收场。因为扣留了那么多“艾未未”们，早已承诺配合到12点，再不放人不好交待。<br />
&nbsp; &nbsp;&nbsp; &nbsp; <br />
我很失望，这还像我们四川的首府吗？我们才从大灾大难中走出来，有的同胞根本就没能走出来，你们甘愿麻木不仁？披着法律的光鲜外衣，享用纳税人的供奉，你们到底怕什么？你们执行不白不黑的指挥，就不怕被自己的孩子们唾弃？同胞们！什么都能失去，万万不可失去的是骨气，是一个中国人的良知和做人的道德底线。你们在让成都蒙耻。<br />
&nbsp; &nbsp;&nbsp; &nbsp; <br />
我很悲哀，被两人押解的作人路过我面前时，我与女儿扑上前拥抱他，被人民警察强有力的手挡住，我们只看见他失望的背影。<br />
&nbsp; &nbsp;&nbsp; &nbsp; <br />
浦志强冲上前拥住我与女儿，我们三人痛哭失声，久久的。<br />
&nbsp; &nbsp;&nbsp; &nbsp; <br />
公开与公平的梦已破灭，公正还会有吗？<br />
&nbsp; &nbsp;&nbsp; &nbsp; <br />
如果每一个中华人民共和国的公民（主人和公仆）都多一点勇敢和爱，那么谭作人们，夏霖浦志强们，今天到场或不能到场的鼓着不息掌声的善良的人们，就可以不流那么多眼泪了。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;2009年8月12日晚上<br />
 <br />
大女儿：8.12见证实录<br />
 <br />
&nbsp; &nbsp;&nbsp;&nbsp;今天，由于塞车，我们到达时已9：20，中院外排起了长长的队，当时不清楚是否都是关注此案之人。原定开庭时间为9：30，眼看就要开庭了，而门检甚严：身份证登记、包安检、身上物品检查。第一个环节，就有人被拒之门外，先是不让我们插队，待我们老老实实排到了，主检官得知我们身份后，主动带我们进去。第一道门检总算通过了。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;进入中院大厅，很多认识的、陌生的热血面孔向我们涌来：“不让进庭！不让我们进庭！”。那也无奈。虽起先说的是公开，申请到旁听证后就能进入，但临时改变也没什么好奇怪的，你永远没办法要求某方做到诚信。 陌生女子给我说话：“你爸爸是英雄，加油！”。带我们进来的主检官开始发旁听证，起先承诺的亲人方7张，结果意外的只发了2张，“亲哥也不能进，不是直系！”“未成年人不能进，哪怕是直系！”我大伯父与16岁的妹妹委屈地被拒之门外。人群愤然，“我们不能进就算了，为什么亲人也不让进！”开始些许骚动。没办法，没时间争执，第二道门检“旁听证”，只有我们两母女拿到了。5号庭门口仍是严检，包包、手机关机交予保管登记。&nbsp;&nbsp;<br />
&nbsp; &nbsp; <br />
 三道严格而善变的门检终于通过，就连律师的助手都没让进。我们进了庭。30个座位的5号庭，放眼一看全不认识，很正常，30个人中，除了我们2个而外，都是被“安排”进来的。随后法官进入：起立。爸爸进来了，半年没见，笑着挥手打了招呼。该来的都来后，很多人关注的审理开始了。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
辩方律师是著名的浦志强和夏霖。浦志强曾是南方都市报“喻华锋案”的辩护人，前不久代理段磊案；夏霖律师曾是崔英杰的辩护人，今年邓玉娇案，他跟另一位律师的努力也有目共睹。<br />
&nbsp; &nbsp;&nbsp; &nbsp;<br />
我因为身份问题，见证了各方人士很想目睹却未能进庭的审判，同时也见证了某种专制和黑暗。浦志强律师身高体壮，一副北方汉子的硬朗，声音浑厚有力，陈词娓娓道来，力度十足。两位律师做了很多功课，准备了很多资料证据。但是，随着女法官无数次打断和阻止辩方律师说话，浦志强律师从激动升级到了愤慨。“为什么不让我说话！辩护人的权利在哪里？！”权力机构很聪明，故意安排女法官，让两位大律师男人不便发飙。看得出女法官也很紧张。尽管律师陈述的都是有力证据，法官还是不管三七二十一地阻止，“我觉得与本案无关，请说下一条。”无语。。。不过法官也只是按指令办事，她的背后是某政权在说话。范晓，艾未未，艾南山三大证人也被阻止了，不准出席，好歹也是三个有名有实之人。被定性为“敌对份子”的“WD”愿回国出庭作证也未被批准。律师要求播放的视频证据也不让放，理由竟是“与本案无关”，“节约资源，时间”之类。如此生硬的理由，着实让人愤慨，一个封建社会的专制法庭。最后，让爸发言，爸说他有一个书面稿，但是因为事件复杂，需要花很多时间陈述，“铁面无私”的女法官立刻打断，“好了，那你就把书面陈词交给我们就行了，你就自己简单讲一下。”其实那些都不难想象，一切都是走过场和形式而已。&nbsp; &nbsp;&nbsp; &nbsp;<br />
 <br />
休庭后，两位大律师爆发了，夏霖向女法官说“现在休庭了，我可以说话了吧！你们真的太过分了！”浦志强说“我觉得 这次是个耻辱，也是你们四川法庭的耻辱！”说完后过来和我们拥抱，高大的汉子竟然哭了，我们一起拥抱着，我轻轻地拍着他，搞不懂究竟是他在安慰我们，还是我们在安慰他。他的哭，在我意识里，是出于在强权专制下的愤慨，毕竟当律师这么多年，从未遇到过这样的压迫。&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;<br />
&nbsp; &nbsp;<br />
夏律师也遭多次打断。发表完辩护词，最后说：四川是人文荟萃之地，多出英雄豪杰。并奉送武侯祠长联：“能攻心，则反侧自消，自古知兵非好战。不审视，即宽严皆误，后来治蜀要深思。”表示相信“四川方面有充分的政治智慧处理好这个案子”。&nbsp;&nbsp;<br />
&nbsp; &nbsp;&nbsp; &nbsp;<br />
我们收拾完律师的东西，还没走到门口，就听到门外轰鸣的掌声，迎接我们的是挤满几层楼的人们。未料到场面如此壮观，以致当时我还没反应过来，处在比较“旷”的状态。震耳欲聋的欢呼声和掌声，就像我们是英雄般。“你爸爸很伟大，你应该为他感到骄傲。”陌生人对我说着。人群里很多人在哭，一些陌生人过来跟我拥抱，他们如此的激动和热忱。那些人群中，有的我认识，大多不认识，是爸的朋友们。还有的来自北川等灾区，没有组织，都是知道消息后，自发赶来的，这点蛮让人感动。出去后，很多摄像机之类对着我们，也许出自哪一方的都有，有的摄像机被便衣抢下，激进一点的被关进车里带走。我出奇的淡定，一个阿姨说“你倒是很稳重。”其实我本身就一点不怕，只是在刚出5号庭时，那种如此壮观和感染人的氛围下，是想哭的，只是忍住了，从来不喜欢在公众地方流泪。人是种很容易被氛围所感染的动物。&nbsp;&nbsp;<br />
&nbsp; &nbsp; <br />
早已料到此次开庭不会宣判结果，结果也难以猜测。已经休庭许久，人们还是不愿离去，此时，人群中很多便衣等出动了，“已经休庭了，快回去！”我们和两位律师合影后，一起去吃饭，只有我注意到了，刚进去就有一桌男人一直看着我们，我知道是之前的便衣或者公安们，坐下后跟肖雪慧阿姨说了。席间，浦志强律师的电话一直响个不停，是来自各方的媒体，浦律师很黑色幽默，接起来问的第一句话就是“请问你们是不是海外敌对媒体？不然我也要跟谭先生一样被定罪了。”浦律师给艾未未打了电话才知道，艾未未被管制起来了，还挨了打，不让出庭，居然连饭也没让吃。直到我们吃完饭正准备离开，艾未未才穿着印有爸头像的白T恤，带着人进来了。其实不只是艾未未，很多人当天都被管制了。看来这次里里外外的下了不少功夫，出动了不少人。那顿饭，居然让旁桌的陌生人提前埋了单，询问才得知是来自灾区的感恩人。高档酒楼与穿着朴素的灾区人们相应成彰。<br />
&nbsp; &nbsp; <br />
在现代社会里，如此公开的专制和打压，真有点前所未见。<br />
&nbsp; &nbsp; <br />
此文，仅以简短的见证与记录，8.12我所亲身经历的场面：那些政权的黑暗，追求正义的勇敢；封建的专制法庭与人性中闪闪发光的感动。&nbsp;&nbsp;<br />
