<strong>王庆华：作人，你还在期待公正吗？――09．8．12庭审后感</strong> <br />
<br />
开庭前两天我去市法院咨询是否会公开审理，得到非常肯定的答复，所以，任何媒体关于此次能否公平、公开、公正审理，要求我谈看法，我的回答都同样是肯定的，起码公开是一定的了。公开可以让不明真相的人普遍受到教育，谭作人那几件事也该让公众清楚。该关该放，既然已进入司法程序，则应依法办事，“我不回避法律责任，也不承担不应负之责任，只为司法公正和社会正义！”谭作人如是说。<br />
<br />
想必二十一世纪入日中天的中华人民共和国，审理一宗反革命案没有道理不公开。 <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;起诉书太空洞、抽象，字里行间透出诸多不严肃，我期盼庭上能有不为人知的钢鞭抛出。（这是基于第一次律师欲见谭作人，办案人不准许称涉及国家机密），起码得与“颠覆罪”对应。谭作人的事早已不仅仅属于我们的家事，怎样判定孰是孰非关乎政府的水平，亦检验社会是想进步或者是相反。所以我渴望，我万分渴望成都政府能体现一贯的大气、和谐、包容，我们的政权固若金汤，我们能战胜天灾人祸。成都是文明之城，文化之城，政府定不会辱没自己的形象，也决不可以低估热爱成都的谭作人们的智商。<br />
&nbsp; &nbsp; <br />
但在进场的那一刻，只有和我的大女儿拿到了旁听证，名额从两天前的不限到当日早上8时许电话中答应的5人，再到因名额有限，只能进3人，最后用果断的声音宣布，“我说2个就2个！”公开这个词，从字典里飞快地被删除。<br />
<br />
庭审从9：40到12：30，我看到和听到的是整个一场公诉方的走秀，他们拿不出我期待的钢鞭。谭作人的回答被审判长数次打断、提醒，只能回答 “是”与“不是”，辩方的证人被告知经审查研究，与本案无关，不准予出庭（其中证人之一的艾未未被打和被控制已是事实）。我看到控辩双方头天傍晚才交换的证据，已被公诉人断章取义、弄得支离破碎，要求辩方律师回答。而辩方律师，每答必被审判长用“不必详细说明，只谈主要意思。”“你如果是。。。。。这个意思，我已明白，按公诉人今天规定的页码”，“我已听明白，下一个问题”。辩方律师忍无可忍，当庭抗议，“有看法下来再交换。”旁边还有定时的帮腔“遵守法庭纪律。”“听从审批长指挥！”。我明白了，但凡辩方律师开口，头顶上定会飘来录音机的反复播放。<br />
<br />
整场都是这样，近3个小时。<br />
<br />
玩笑开大了，基本上是国际玩笑！<br />
