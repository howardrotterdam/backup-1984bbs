我问我的邻座：“你了解这个案子不？”他马上浑身僵直，呆若木鸡，我不甘再问：“哎，问你。。。”他仍然当铜像。我基本明白了周围的角色。我不再看谭作人的背影，只专注地注视女检察官和她旁边那个会说话的木偶的嘴。因为我已清楚，只要辩护律师一说话，台上那几位穿着行头的道具就会发出早已编好的台词。<br />
