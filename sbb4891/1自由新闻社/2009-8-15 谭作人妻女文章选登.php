<strong>柔肩担道义 丹心护公正</strong><br />
――记成都市中级人民法院刑一庭审判员刘菡<br />
<br />
发布时间：2007-11-26 18:52:34<br />
<br />
<p align="center"><img src="http://i25.tinypic.com/351zqsy.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /></p><br />
<br />
&nbsp; &nbsp; 刘菡是成都市中级人民法院刑事审判第一庭的一名普通党员，一位普通的审判员。多年来，她坚守在法院平凡而艰苦的工作岗位上，认真履行职责，爱岗敬业，默默奉献，始终坚持司法为民、公正司法、清正廉洁的要求。十余年来，刘菡共审结各类案件六百余件，没有一件超审限案件，无一件错案，被称为群众合法权益的“保护神”。<br />
&nbsp; &nbsp; 作为一名共产党员，一位普通的刑事审判法官，她用自己的执著守护着法律的公正，捍卫着群众的利益。十几年来，她面临种种诱惑，经历种种困难，始终守住心中那片净土，用一腔真情抒写了一位普通法官不负天平无愧国徽的赞歌。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; 加强思想政治学习，提高职业素养<br />
&nbsp; &nbsp; 在工作中认真学习马列主义、毛泽东思想、邓小平理论和党的“十七大”的有关精神，努力实践“三个代表”重要思想，牢固树立全心全意为人民服务的宗旨意识。近年来，在政法系统开展了声势浩大的集中教育整顿活动，我院召开了专门会议部署了队伍集中教育整顿的活动安排，进行多次集中学习，贯彻活动精神。刘菡同志认真学习了规定的文件和领导的重要讲话。通过学习，充分认识到集中教育整顿的必要性和重要性，克服了消极、畏难情绪，从思想根源上查找问题，积极解决，并深刻地认识到刑事审判工作是保护、发展和实现最广大人民群众根本利益的需要，是构建社会主义和谐社会的重要一环，要切实把它作为一项义不容辞的重大政治任务，在此基础上，增强了责任感和使命感、强化了政治意识和法治意识。<br />
&nbsp; &nbsp; 刑事审判作为一项专业性很强的司法工作，必须要有扎实的理论修养。1991年刚走上刑事审判岗位的时候，面临最大困难是不熟悉刑事审判业务，为了尽快实现由“外行”向“内行”的转变，刻苦学习法律知识，不断提高自己的理论水平，在繁忙的工作之余，参加了四川大学的在职研究生班学习，并以优异的成绩完成学业，1999年首批被任命为审判长。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;公正审判，维护社会治安<br />
&nbsp; &nbsp; 作为中级人民法院刑事审判庭的法官，手中掌握着生杀大权，肩负着维护社会稳定、保护人民生命财产的重要职责，绝不允许出现任何差错。刘菡同志深刻的理解中院刑事审判的特殊性，认真钻研刑事方面的法律、法规，提高办案水平，没有出现过因审判业务不熟而给工作带来负面影响的情况。具有针对性和系统性的开展工作，充分发挥主观能动性，妥善处理各种关系，完成领导交办的各项任务，并及时向庭领导汇报有关问题和情况。坚决防止违反审判程序规定的事情发生，熟练地主持庭审，准确地归纳控辩双方的辩论焦点，突出重点，控制庭审的节奏。在“严打”整治斗争，刘菡同志克服家庭困难，夜以继日地加班加点工作，保证了审判工作的顺利完成。作为审判长，刘菡认真做好本职工作，多年来办案数量居全庭前茅，还审理了一系列的复杂、疑难、重大案件案件。如审理了在全市具有重大影响的被告人周某某等人、王某等人被控组织、领导、参加黑社会性质组织罪，公安部督办的熊某某等人故意杀人案（即8.12枪击案）和王某等人组织卖淫案，廖某某等二十五人抢劫、盗窃、销赃、转移赃物案等。在审理涉黑、涉恶案中，由于该类案件的社会关系较为复杂，案件的审理受到了许多不正当的干扰，威胁利诱的电话接连不断，在这种情况下刘菡坚决的抵制来自各方面的压力，依照法律作出了公正的判决，取得了良好的社会效果，为成都市社会治安的稳定发挥了积极的作用。其实不仅在审理重大的案件中一丝不苟，就是在审理一般的刑事案件中也做到心细如针，刘菡表示：在处理案件过程中，首先要讲法律，在此基础上，还要讲道理，讲情，妥善处置各方的矛盾，兼顾“法、理、情”，法官既要有严肃的一面，也要有爱心，一定要尊重人，才能把案件办好。在2006年审理的汤某某被控故意伤害案中，由于被告人是因为被害人和他的妻子有不正当关系，才殴打被害人致其死亡的，被告人的家属依靠朴素的情感意识认为事件的发生情有可原，不应当判处被告人重刑，反复的打电话和找各种关系要求从轻处罚。但是被害人的家属又认为被告人和其妻子的关系长期不合，被害人没有过错，被告人的行为不是故意伤害致人死亡，而是典型的故意杀人，要求对被告人予以严惩，并扬言其要求得不到满足就要组织人来法院上访。在开庭审理的时候，双方家属的情绪都比较激动，一度扰乱正常的庭审秩序，针对这种情况，庭审后刘菡多次和双方当事人沟通，认真的给他们讲解故意杀人罪和故意伤害罪的界限以及相关的法律规定，使他们认识到被告人行为所造成的严重后果以及被害人确实存在一定的过错，并在此基础上积极促使被告人答应向被害人的家属作出一定的赔偿，最后他们都表示相信法院会作出公正的判决，宣判后双方当事人都没有上诉，也没有引发其他的不良后果。<br />
&nbsp; &nbsp; 作为刑事法官，在审理刑事案件时要严格把握证据，刑事案件对证据的要求是排除合理怀疑，特别是对有可能判处被告人死刑的案件，更是要作到排除一切合理怀疑。但对这一点当事人，特别是被害人的亲属却不能理解。此时，刑事法官就要承受更大的压力。2003年，刘菡他们合议庭审理了一件故意杀人案，被告人和被害人是同学，被告人是在单亲家庭里长大，父母对其关心不够，因嫉妒被害人有一个幸福的家庭热爱他的父母，有一天在被害人家里残忍的将被害人杀害。但该案证据上有一个严重的瑕疵――在案发现场的衣柜里发现了一枚第三者的血指纹，该血指纹既不是被告人和被害人的，也不是进过现场的被害人亲属和侦查人员的。由于这一瑕疵，加之被告人刚满十八岁，是在校学生，遂对被告人判处了死刑，缓期二年执行。宣判后，被害人亲属不理解，坚决要求法院判处被告人死刑立即执行，直到现在还经常到法院威胁刘菡同志，并扬言伤害其子女。刘菡同志就是这样顶住压力，坚持原则，秉公执法。<br />
&nbsp; &nbsp; 在文书制作方面，刘菡同志制作的法律文书做到了叙事清晰，说理严密，文字表达流畅，裁判结果准确，引用法律条文全面的要求，没有出现过因法律文书的差错而引起不良影响。<br />
&nbsp; &nbsp; 刘菡反复强调，法官并不是什么官，而只是社会的一个裁判者，是国家和人民赋予了法官审判资格，法官应该用一种平和的心态公平地去进行裁决。我们要把党和人民给予的荣誉和信任转化成为源源不断的动力，转化成为一生的责任，始终保持清正廉洁的工作作风，更好地做好刑事审判工作，为社会公平正义贡献自己更大的力量。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; 淡泊名利 充实人生<br />
&nbsp; &nbsp; 刘菡非常喜欢的一句座右铭是“砝码稍沾灰尘，便会失去公正”，从自己做起，公正地审理每一件案件，作一个人民信得过的、品德高尚的好法官，为维护公平、正义的法制环境，贡献出自己的一份绵薄之力是刘菡一生追求的目标。<br />
刘菡同志严格执行党风廉政建设的有关规定，无贪污受贿、徇私枉法、隐瞒和伪造证据的行为，遵守保密纪律，认真办案，绝不向当事人借钱、物等，远离营利性的经营活动，从不利用职权为亲属谋利益，不在私下场合会见当事人、接受当事人及代理人的请客送礼。审判作风正派，未办理“人情案、关系案、金钱案”。无利用职权“吃、拿、卡、要”，坚决杜绝了有悖于廉政的现象发生。<br />
&nbsp; &nbsp; 在日常生活中，刘菡同志严格遵守《法官法》、《法官职业道德规范》的规定，严格按照本院《法院管理规范》、《法官行为规范》的要求，坚决防止违背“十条禁令”、“十三个不得有”的情况发生；始终认真贯彻“司法为民”的宗旨，积极服务于审判这一中心，工作作风正派；在承担具体工作时，服从领导安排，顾全大局，不计较个人得失；遵守《公民道德规范》的要求，是非分明，和同事之间相处融洽。涌跃参加院里组织的公益活动，捐款、捐物帮助贫困地区的失学儿童和城镇下岗工人，奉献自己的爱心。作为一名女法官，做到了自尊、自强、自立，尊老爱幼，家庭和睦，团结邻里， 2003年度被评为“五好家庭”。<br />
&nbsp; &nbsp; 为了审判工作，她付出了全部的精力，当然有付出就有回报，近年来均超额完成了审判任务，并在2000年、2002年、2003年本院的“先进工作者”；2002年、2003年、2004年连续三年年终考核为优秀；2003年被评为成都市的“严打先进”和2004年被评为成都市劳动模范。那一枚枚闪光的奖章和那一本本鲜红的证书，正是她对自己人生的写照。<br />
　　“位卑未敢忘忧国”，刘菡虽是一名普通的女法官，她却承担着分辨邪恶、伸张正义、秤量公道之重任。为保一方人民的平安，她热爱自己的岗位，热爱自己的事业，她用纤细的身体托起了法律的尊严，以柔弱的双肩扛起了共和国的天平，用一双玉手擎起了一片法治的青天。她常说，为人民服务是永无止境的，她甘愿象翠竹那样具有清风高洁的品格：一身正气，不辱使命；纵然清色，无愧无悔。她更象是翠竹中的一株，默默无闻，奉献社会，奉献人民。<br />
