王庆华：我想说几句话<br />
 <br />
&nbsp; &nbsp;&nbsp;&nbsp;今阅大泽兄的打油诗“汨罗江上沉屈原，风波亭中斩岳飞，三千年来忠魂苦，最苦成都谭作人。”感慨万分。<br />
 <br />
&nbsp; &nbsp; 二十多年了，谭作人对国家的大忠远远多于对小家的小孝。他数次哀叹：太对不起家人了！我劝慰他：自古忠孝难两全，大忠即为大孝。<br />
 <br />
&nbsp; &nbsp;&nbsp;&nbsp;谭作人入狱后，我们不满十六岁的小女儿五月十二日曾去信写到：“爸：五月十五快到了，提前祝你生日快乐，并给你讲一个儿时听过的童话故事。在一个国家，有一个国王和三个公主。一天国王问三个公主：‘你们有多爱我？’大公主说：‘我象爱蜜糖一样爱您。’二公主说：‘我象爱花朵一样爱您。’国王听后十分满意。问到三公主时，三公主说：‘我象爱盐一样爱您。’国王一听勃然大怒，立刻下令将三公主关进牢房。故事讲到这里，我不禁一阵悲哀。不知三公主被她最爱的父亲关进牢里，心中是怎样的凄凉？明明如此爱着父亲，只是爱的方式不同，不象其他公主那样甜言蜜语，就被下了大牢。三公主何罪之有？其实这个故事有个相当好的结局。一个邻国的国王听说了此事，便向三公主的父亲发出邀请参加一个盛典，席间很多佳肴。一尝，味如嚼蜡。这时有人告诉他：菜里都没有放盐！国王恍然大悟，原来渺小的盐不甜不香，却是最必不可少的一部分。国王马上回宫下令释放三公主。从此以后，三公主得到国王最多的宠爱。这是有名的童话《盐之公主》。故事有个好结局，但现实生活中的结局令人叹息。国王能良心发现吗？悲哀！你的家人和朋友、猫猫狗狗、花花草草依旧思念你，愿大家的关心能如清风，在炎夏给你带来一丝清凉。你的女儿。”<br />
&nbsp; &nbsp;<br />
作人十六日回信：<br />
&nbsp; &nbsp; <br />
“我的女儿：此间规则为逢十发信，但我等不及了。面对你送上的生日礼物――一封充满大爱的家信和100元钱，我失眠了。我无数次披衣而起，仰望铁条分割的夜，想念同城异地的你！此刻的我，再一次明白了一个道理：有的‘近’，其实很远；有的‘远’，实在太近！我们分别已经50天整，但我却实实在在地感觉到，我每一天都能看见你……一代人有一代人的价值，一个人有一个人的命运。我们这一代人，是集体主义的产物。这是宿命也是一种使命――因为集体。有集体的或者公共的价值需要传递下去。有人选择了这件工作，使命就可能成为史命――一种历史的创造和命定。这并不等于说个体的价值不重要。在二元乃至多元的文明中，集体和个体各自存在，相安无事，共生共存。在我为人人向人人为我的转型期中，需要两个以上的声音。当三公主的盐成为稀缺资源的时候，我们这一代人，应该有人惭愧。因此，三公主选择了看见和说出，他不会感到委屈，请你放心。心，就是为了给予，伴随着一滴眼泪（泰戈尔）。这支歌曲，是大家的。这滴眼泪，是自己的――或者说，是自己和自己的家人共有的。我可以毫无愧色地说：我对得起这个社会。但我对不起你们――我最亲最爱的人。你们，就是我的眼泪。生命的量变是一天天减少，生命的质量是一天天提高。而生命升华成为精彩，必有一种大爱在其中，从心底发散出来。即使是昙花一现和浮光掠影，梦幻似地存在，它也使你充满光明，不再惧怕得失与生死。这是一种大自由的境界。现在想来有些歉然，又有些释然――我们之间从来没有刻意探讨或交流过什么，但你竟然能够理解这一切。你的理解，让我十分感动。你的理解，已经超越了‘小爱’。……但愿这一次‘灾难’能使你也转危为机，产生一个大的飞跃！……想念你的爸爸。”<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
直至今日我仍然坚信，他那么爱的国家无论如何不会判他“颠覆”之罪！一个强大的国家真的容不得正直的人说几句真话？请不要让孩子们生活在困惑中，更不要让他们失望！谭作人们，该苦到头了！&nbsp; &nbsp;&nbsp; &nbsp;<br />
 <br />
 2009、8、6</div>

							
							
							
							
