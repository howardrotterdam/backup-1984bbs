　　Zhen Qianling, a chemist among the crowd, said the stink from the plant on hot days made him feel sick and sent his heart racing. 'We want to block the traffic so the government will hear our voice. If we just sit back and do nothing, the government will also do nothing.' Like many, this was the first protest he had joined. The demonstrators were young urban professionals - designers, internet workers and translators. Other protestors were from the 'New Sky Universe' and 'Berlin Symphony' tower blocks. Property costs about 14,000 yuan (&#163;1,100) a square metre, well above the Beijing average. The residents thought they were buying into one of the city's most salubrious neighbourhoods, but on hot summer days, when the wind is in the wrong direction, their homes are filled with the stench from the dump.<br />
<br />
　　Zhen Qianling是人群中的一名药剂师，他说垃圾厂大热天散发出的恶臭让他恶心，心跳加快。“我们要阻塞交通让政府听到我们的呼声。如果我们坐视不管，政府也不会有所作为”。和许多人一样，这是他第一次参加抗议。示威者是市区年轻的专业人员――设计师、IT人员和翻译。其他抗议者来自万象新天和柏林爱乐这两个高密度小区。这两个小区的房价已经达到每平方米14,000元（1100英镑），大大超出北京平均水平。这些居民本以为买到了市内最生态环境很好的小区，但每到炎热的夏季，如果风向不对，家里就会充斥着垃圾场的恶臭。<br />
　　<br />
　　'If I had known, I would never have bought a home here,' says Helen Liu, a translator who moved into her 500,000 yuan house in April.<br />
<br />
　　“早知如此，我才不在这里买房。”Helen Liu说。她于今年四月搬进这套价值50万元的房子。<br />
　　<br />
　　In the run-up to the Olympics, police detained several prominent dissidents and put others under close surveillance. Three 'protest parks' were established, but of the 77 people who applied to use them, none have yet succeeded. According to human rights groups, several applicants were sent back to their home provinces or put in 're-education through labour' camps. Foreigners who staged Free Tibet demonstrations have been deported.<br />
<br />
　　在奥运的准备阶段，警方扣留了几名突出的异议者，并严密监督其余多人。三个“抗议公园”建成了，但是77名申请使用者无一成功。据人权组织称，几名申请者被遣返回乡或是被送进劳改所。举行自由西藏示威的外国人则受到驱逐。<br />
　　<br />
　　The residents of Chanying said they did not fear a police backlash because China was becoming more open and the authorities' concerns about losing face during the Games have diminished.<br />
<br />
　　常营地区的居民说他们不怕警方报复，因为中国越来越开放，而且当局也不像奥运期间那么担心丢面子了。<br />
　　<br />
　　During the demonstration and after police warned the protesters they were breaking the law, they became almost comically well-mannered. They walked slowly back and forth for more than an hour across a pedestrian crossing - but only on the green man - chanting, 'We don't want stinking air.'<br />
<br />
　　在示威时以及警方警告抗议者违法后，他们变得近乎可笑的规规矩矩了。他们在人行横道上来来回回一个多小时，但只在绿灯亮时大呼“我们不要恶臭空气。”<br />
　　<br />
　　Managers at the site said emissions met environment bureau standards, but officials acknowledged the smell was a problem. 'We pay a lot of attention to the residents' concerns,' said Guo Tuanhui. 'On hot days, the buried rubbish gives off a bad odour. But we are doing what we can.'<br />
<br />
　　当场的管理人员说排放达到环保局标准，但官员们也承认臭气是个大问题。“我们也很关心居民的感受。”<br />
　　<br />
　　The rally appeared to be part of a growing trend in China, as well-educated, middle-class citizens complain about environmental hazards.<br />
<br />
　　由于受过良好教育的中产阶层公民对环境公害的申诉，示威游行在中国风潮日盛。<br />
　　<br />
　　In May 2007, thousands took to the streets of Xiamen in Fujian province, forcing the local government to halt plans for a chemical factory.<br />
<br />
　　2007年5月，数千人走上福建厦门的街头，迫使当地政府停止建造化工厂的计划。<br />
　　<br />
　　Last year, the head of China's environmental agency, Zhou Shengxian, blamed the rising number of riots, demonstrations and petitions across the country on public anger at pollution.<br />
<br />
　　去年，中国环境保护部部长周生贤谴责全国各地因污染问题引起公愤，酿成不断增加的骚乱、示威和请愿活动。<br />
　　<br />
　　The public have good reason to be concerned. According to the World Bank, up to 400,000 people in China die each year from outdoor air pollution, 30,000 from indoor air pollution, and 60,000 from water pollution.<br />
<br />
　　公众的忧心忡忡不无道理。根据世界银行的数据，中国每年死于室外空气污染者达40万人，其中3万人死于室内空气污染，6万人死于水污染。<br />
