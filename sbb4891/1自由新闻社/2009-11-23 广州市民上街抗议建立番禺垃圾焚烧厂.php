													<h2>Reuters ：Hundreds in south China oppose waste incinerator</h2>
						
						
																											<div id="postmessage_338402" class="t_msgfont">Sun Nov 22, 2009 11:56pm EST<br />
<br />
HONG KONG (Reuters) - Hundreds of residents in China's southern city of Guangzhou protested outside government offices on Monday, opposing plans for a major garbage incinerator that they say will endanger their health and the environment.<br />
<br />
Wen Yunchao, a prominent mainland blogger and rights activist, said more than 1,200 protesters had demanded the resignation of the city's deputy general secretary, Lu Zhiyi, during a large protest outside Guangzhou Municipal Government building.<br />
<br />
He said the Panyu waste incineration plant, which will handle 2,000 metric tons of trash a day, might endanger the health of residents like a similar incinerator built near Guangzhou's Likeng village in 2005.<br />
<br />
&quot;The residents don't trust the government's guarantees after reports revealed that the incidence of cancer had risen dramatically in Likeng,&quot; Wen told Reuters by telephone.<br />
<br />
Nearly 92 percent of residents believe the project will seriously harm their health and the environment, while more than 97 percent oppose construction of the plant, according a public opinion poll by the provincial social research and study center in Guangdong province, China's manufacturing heartland.<br />
<br />
The provincial capital, Guangzhou, has experienced serious environmental degradation after nearly three decades of break-neck development. Environmental activism, however, has grown in recent years as the city's burgeoning middle class pursue a higher quality of life.<br />
<br />
In a recent case, a proposed multi-billion dollar oil refinery in the ecologically rich Nansha district just downstream from Guangzhou along the Pearl River, was relocated to a less populated area in western Guangdong after a major public uproar.<br />
<br />
&quot;We are here today to ask for transparency in policy decisions,&quot; said Lu, a local resident at the protest, who did not want to give his full name.<br />
<br />
Another resident and blogger using the name King Bird, who recently submitted a petition paper to local authorities, said: &quot;It is a very populated area, with many residential developments. Having bought flats there, we don't now want to get sick.&quot;<br />
<br />
