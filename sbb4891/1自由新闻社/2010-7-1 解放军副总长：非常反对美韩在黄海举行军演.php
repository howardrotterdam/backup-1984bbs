<br />
　　梁茵：在这个美韩军演的这个整个的事件从六月初就开始吵吵嚷嚷的，这个消息尽管它是一再的推迟，但是这个最触动中国人的神经的就是航空母舰，就是乔治-华盛顿号。但是美国人似乎在这个华盛顿航母问题上也是支支吾吾，一会儿说来，一会儿说又不来，现在你要看，它又返回去了。我们就想知道美国人到底在用航母在做什么？它是想刺探中国人的反映吗？如果你真的开来了，从我们军方的观点看，我们真的会害怕你吗？<br />
<br />
　　罗援：对于航空母舰这个事，我可以说两句话。第一我们是坚决反对，第二我们是不怕。坚决反对，我们刚才已把我的理由表达了。<br />
<br />
　　梁茵：有刺探的技能。<br />
<br />
　　罗援：从几个方面来看，我们应该是要反对的。从不怕来看，我们可以回顾一下历史，美国的航空母舰也多次到我们的领海附近进行巡弋，或者是一种威慑。大家都记得1958年的823炮战，那次美国来了多艘航空母舰，但是毛泽东根本就不理他这一套，打打停停，停停打打，情况对我有利就打，情况对我不利我就不打。而且当时提出的一个就是只打蒋舰不打美舰，直接打蒋间接打美。结果我们一朝蒋舰打炮，美舰赶快跑到12海里以外，它说这是公海，你不能再打我了。毛泽东就得出一个战略判断，美国在台海地区它是采取的战略守势，而不是战略攻势，这是一个历史记忆。<br />
<br />
　　还有一个历史记忆，就是96年的台海危机。美国派两艘航空母舰到我们的台海地区进行军事威慑。当时我们正在进行导弹实射演习，当时我们的前几发导弹是在美国航空母舰来之前我们打的，后几发导弹是美国航空母舰来了之后，我们照旧打的。<br />
<br />
　　美国人得出了一个战略结论，就是一旦美国进行军事介入，中国人民和中国人民解放军的决心和意志绝不动摇，所以说在这问题上，我们不怕。现在如果美国再派乔治-华盛顿航空母舰到我们的近海，就是黄海地区，你说到底谁更怕一些？<br />
<br />
　　梁茵：就是58年的时候，我们都不怕，今天从我们的的军事力量来说，我们更不怕。<br />
<br />
　　罗援：从这个方面来讲，它给我们提供了一个活靶子，给我们提供了一个知己知彼的机会。为什么说给我们提供了一个知己知彼的机会呢？你打仗要知己知彼才能百战不殆。首先我要知己，就是你的航空母舰来了，我要先检验我的一些侦查预警和感知系统灵不灵，它能不能看到你，这个我们可以进行一个检验。<br />
<br />
　　另外，对我的一些连通系统，它能不能连的上，再一个我们的快速反应能力，你能不能做的非常灵敏便捷，还有我们的联合打击能力，它能不能打的准，打的狠。这就是对我们的作战能力是一个检验。<br />
<br />
　　另外，你既然送上门了，我们也可以对你逼近观察一下，我们可以看一下你的航母编队，你这个航母的最佳的作战阵位在什么地方，你的航母编队是怎样编成的，你的这些通讯联络包括你的作战技术指标，我们都可以进行逼近观察，以前老是说中国对你进行了侦查派间谍了，现在我们不用了，你送到我们家门口了，我当然可以看一下了。<br />
<br />
　　以前我们搞军事演习，我们还要搞这个红蓝对抗，还要找一个蓝方，还要有个成本，现在成本不要了，现在美国直接给我们当蓝方。<br />
<br />
　　梁茵：我们可以实战演习了。<br />
<br />
　　罗援：我们就可以进行实地的模拟的演习，所以这个美国应该更怕一些，如果美国要把这个担忧去掉，你最好的办法就是你最好别来。<br />
<br />
　　梁茵：所以说，我们现在也怀疑，它现在这个航母是返回去了，而且对这个航母到底来不来，它现在也没有明确的答复。其实是它自己心虚的。<br />
<br />
　　我们还想请罗将军详细的分析一下，如果它这个航母真的到我们这个近海来了以后，它哪些方面，它航母舰队的哪些方面，航母的哪些技术能够暴露在我们面前，我们怎样给它做一个探底？<br />
<br />
　　罗援：比如它的C4ISR系统，它的结点在什么地方，它要进行演习，它就要把这些问题要全部都开通，它要进行实际，那就给我们提供一个机会，这个C4IS R系统，以前我们还没有近距离观察它们的一个机会，现在有这么一个机会。<br />
<br />
　　另外，和韩国进行联合军演，它的通讯使用的一些文本、频道，这个也是保密的，虽然这次开通的肯定不是作战用的通讯频道，但是我们基本知道他们通联的基本方法是什么样的。<br />
<br />
　　另外，它们要进行联合军演，它们在什么阵位上，航空母舰不是所有的位置都可以去，有的地方可能不能去，有的是它的最佳作战阵位，我们也可以进行这种观察，这不是我们有意的去刺探这个军情，是你送上门来的，你让我们看。如果你不想暴露这些秘密，我觉得你你最好的办法就别来。<br />
<br />
　　梁茵：你也别那这件事再忽悠我们。<br />
<br />
　　罗援：你也别拿它说事。<br />
<br />
　　梁茵：好，非常感谢罗将军。</div>

							
							
							
							
