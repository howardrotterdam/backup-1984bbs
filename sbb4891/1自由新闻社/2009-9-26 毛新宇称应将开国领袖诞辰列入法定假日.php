													<h2>毛新宇称应将开国领袖诞辰列入法定假日</h2>
						
						
																											<div id="postmessage_258387" class="t_msgfont">毛新宇称应将开国领袖诞辰列入法定假日<br />
<br />
2009年09月26日 01:05信息时报<br />
<br />
毛新宇出任广州松田职业学院名誉院长，并与学生、记者进行交流。信息时报记者 巢晓 摄<br />
<br />
信息时报讯 (记者 薛冰 通讯员 李帆 温丽丽) “我还不是少将，只是大校！”昨天，毛泽东主席的嫡孙、毛岸青与邵华之子毛新宇出任广州松田职业学院名誉院长，并首次澄请此前关于他是少将军衔的传言。同时，他表示国家应将开国领袖的诞辰列入法定假日。<br />
<br />
作为后备军任名誉院长<br />
<br />
“毛新宇是我的后备军，我不能到松田上课了，就派新宇来。”2007年9月21日，开国领袖毛泽东主席儿媳邵华将军出任广州松田职业学院首任院长时向记者如是说。<br />
<br />
去年6月24日，邵华将军溘然长逝，但伟人家风长存松田校园。昨日下午，毛泽东主席的嫡孙、毛岸青与邵华之子、全国政协委员、中国人民解放军军事科学院战争理论和战略研究部副部长毛新宇博士，正式出任广州松田职业学院名誉院长，并出席松田学院、松田职业学院2009级新生开学典礼暨军训总结大会。毛新宇再借用爷爷毛泽东的话，寄语松田学子好好学习，爱国爱校。<br />
<br />
首次澄清:还只是大校<br />
<br />
“我们是称呼您毛将军，还是毛少将？”“叫我毛部长就行！”面对记者询问，毛新宇早有准备。他说:“我目前军衔还只是大校，不是少将，在此特别澄清！”他解释，他只是中国人民解放军军事科学院战争理论和战略研究部副部长，副军职干部。之所以被媒体误读为副军级是少将军衔，原因是他此前参加毛泽东生平与研究峰会和湖南国际旅游节活动，地方误理解为省里的副军级都是少将军衔。<br />
<br />
毛新宇说，“军队授衔有严格规章制度，需要到一定年限，我2000年入伍读博，后留军队工作，如今不到10年，时间和资历还不够，正常情况要到明年‘八一’前满十年，才达到基本年限，才有资格考虑。”<br />
<br />
记者发现，即便明年“八一”前毛新宇被授少将军衔，他也是新中国最年轻的将军。<br />
<br />
谈国庆阅兵<br />
<br />
今年在观礼台上看国庆阅兵<br />
<br />
60周年大庆阅兵即将举行，作为伟人后代，毛新宇及其家族成员会否出现在观礼台上？毛新宇透露，“国庆阅兵当天，我会在阅兵观礼台上，家人会不会去，带不带儿子，不是我说了算，中央有规定，可能是我一个人去。”<br />
<br />
毛新宇说，新中国成立60周年，对毛家来说是特别日子，毛家共牺牲10多位烈士。前不久，中央评选“双百”人物，家族有4位烈士当选，分别是奶奶杨开慧，毛泽民、毛泽覃、毛岸英。<br />
<br />
最近，他正在写纪念奶奶杨开慧的文章，开头用的是《蝶恋花・答李淑一》中毛主席纪念她的诗词，文章刚写完，5000多字。伯父毛岸英牺牲在朝鲜战场，因此每年抗美援朝纪念日自己都会去悼念他。<br />
<br />
谈爷爷<br />
<br />
毛泽东诞辰应纳入法定假日<br />
<br />
每年9月9日毛泽东祭辰与12月26日毛泽东诞辰，家人都会到毛主席纪念堂“看望”。毛新宇说，对开国领袖生辰祭日应该广泛宣传，让后人铭记。开国领袖的生辰或祭日应该放假，以特殊方式纪念，要不然时代久远，将来的孩子连毛泽东是谁都不知道。现在，中小学课本大不同，关于革命先烈事迹的课少了很多，应予以恢复，“我正在写一个有关传统教育的提案，要求将12月26日毛主席诞辰作为特定节日，明年全国两会上正式提交。”“希望家族多些英烈事迹进入中小学课本。”<br />
<br />
谈毛泽东思想<br />
<br />
申请高校设毛泽东思想系<br />
<br />
说到如何继承爷爷志愿，毛新宇说自己和松田学院马云珍院长交谈，就鼓励大学设立“毛泽东思想系”，目前已向国家教委提出申请，正在审批，明年能否在高校中开设相关课程还很难说，但会不懈努力。<br />
<br />
毛新宇认为，国家教育部一向重视毛泽东思想在高校中的教育力度，相信应该会得到批准。如果审批通过，会在教育部门指导下开好这门课程。<br />
<br />
毛新宇坦言，目前自己不仅是军事思想研究人员、全国政协委员、几届全国青年常委，还有另一个身份――教师，受聘为全国几十个大学客座教授。作为伟人后代，自己很想把毛泽东思想研究内容推向世界，目前正在尝试将自己所写研究书籍翻译成外文。<br />
<br />
谈儿女<br />
<br />
望儿子承父业研究毛泽东思想<br />
<br />
伟人后代如何教育孩子？昨天，毛新宇谈到教育子女问题时说，平时工作很忙，但会尽量抽出时间陪孩子，教儿子东东写字、背诗、念成语故事。现在，小孩子要学的东西很多，除了英语，还要学画画、弹钢琴，但不学奥数。儿子东东明年就要上小学，作为父亲，期望孩子能子承父业。“作为领袖后代，出生在革命家庭，除了解祖国的历史、党的历史外，更应对曾祖父毛泽东思想系统有全面了解。希望儿子东东全面发展，像我一样研究毛泽东思想。”<br />
<br />
至于女儿毛甜懿，才1岁多，“她还太小，不想为她规划太多。”</div>

							
							
							
							
