																											<div id="postmessage_861091" class="t_msgfont">以下是本人10月8日晚上亲眼所见以及亲身经历的事件，在这里说出。可以说是给大家以后再有类似的聚会活动有个参考吧。<br />
时间：10月8日18:20――22:15<br />
地点：人民广场喷泉，新世界门口，饭店<br />
 <br />
在写抓人事件之前先对自己做个简单的介绍吧，算是在1984上和大家都认识一下，交一个朋友了。本人网名马玛，年龄20，单身。今年7月结束大二的课程后，就一直在上海了，开学就没回去上学了，现休学在上海闸北做生意，也算是创业吧。算是自己寻找自己的一种活着的方式了。<br />
<br />
事情经过：下午5点在群内收到刘晓波获奖的通知，当时和大家一样都很兴奋，说不上来这种兴奋感是从何而来，是因为狠抽了中共一嘴巴子，还是为中国民主事业的明天有了一丝的曙光而开心。说真的到现在还不是很清楚。不管了，这不重要，还是先像其他人很高兴一样，先高兴一番。然后很装B的在校内上发布自己的心情和刘获奖的事情。之后所在的1984上海联谊群有人建议晚上大家聚聚一起吃个饭，之后发现推友还有sina围脖都有组织在人民广场喷泉聚会的（现在感到奇怪为什么都选在那里，到了之后才发现上海市政府就在对面）。正好当时情绪比较亢奋，马上回复了，记下联系人的电话。（所在的群因为潜水比较多一直都没有说话，所以大家彼此都不熟悉）<br />
&nbsp; &nbsp; 因为才来上海3个月不到，地点都不太熟悉。怕去晚了，到时候找不到组织。所以就提前去了，后来到了后一看表才18:20，估计我是到人民广场聚会的人中最早的了，去了后还没有发现什么不对劲的事，说实话，这也是我第一次参加这样的聚会。来早了也没事干，就在周围转转，感觉一切都挺正常的，周围是有2-3辆警车的。一直晃荡了将近30分钟。在这之前人民广场的喷水池周围一直都是有人的，好像这里算是人民广场比较标志的位置，等人的或者朋友聚集都在这里。来来往往人不算多，大概始终保持在50人以上。<br />
&nbsp; &nbsp; 接着高潮出现了，在18:45的时候（时间应该就是在这个时候）。国保开始行动了（他们穿的便衣，有一个右手打着石膏，总共大概有4个人，其他的就是穿制服的巡警了仅在喷水池的有6个，后来还出现了4个人穿制服的武警）有3个参加聚会的朋友（来聚会的很多都是互相不认识的，认识的只是少数的）被广场的巡警前来询问。要求查他们的身份证，被查的3个聚会人员年龄大概在40岁左右，有背着背包的（不知道是不是包里面有什么东西才被带走的）估计他们也是有经验的，先是要求看警察的警官证件。双方都很平静，接着没有穿制服的国宝出现了。他们直接拿过受查者的身份证，受查者见其没有穿警服，又是突然出现的。便直接夺回身份证，他们当然知道那批人是来干嘛的，不过故意对其身份怀疑。接着双方就是进行了一番言语冲突。国宝问他们是干嘛的，受查者说是等朋友的。国宝直接就说把人带走，然后受查者反问“你凭什么抓我”――“你说我凭什么抓你”&nbsp;&nbsp;就这样第一批人被国保强行带到了警车里面，是3个人都被带走了，还是只带走一个人，我没有注意到。我在人民广场喷水池的2个多小时见到的一共有4次国保前来询问，然后带走人。先写这么多吧，回来再更新<br />
<br />
继续更新一些吧，期间我看到的国保抓人总共有4次，一共有7-8人被带走，期间国保并没有使用暴力，双方也没有肢体冲突（仅是我看到的）。每次他们被带走的时候我都在旁边，因为一个喷泉广场也不大，只要国保要抓人了，几乎就有一些不明真相群众围观上去了，好吧那也冒充不明真相群众一起上去围观吧，虽然眼睁睁的看着革命同志一个个被带走，但我也只能这样目送他们远去，只能希望国保只是扣他们2小时就放人吧。可能我比较年轻，长相也符合遵纪守法好公民的要求吧。一直在警察旁边转悠可是也没见到有警察上来询问我。<br />
<br />
