from 法国国际广播电台<br />
Oct 24, 2009<br />
<br />
不久前，有一个场面，通过媒体的报道和互联网影音共享网站YouTube向全世界转播，感动了千千万万人： 2009年10月7日，在美国首都国会大厦会议厅，一场不寻常的舞会正在举行，参加舞会的有国会众议员、众议院中国事务委员会成员皮特斯，众议员史密斯，舞会的主角是在89年六四屠杀中被解放军坦克碾断双腿的方政。欢快的舞曲奏响，安上了智能假肢的方政与妻子翩翩起舞。<br />
<br />
方政今年2月6日，经由美国旧金山华人“人道中国”组织的救援，从中国来到美国。仅仅7个多月的时间，20多年来只能坐在轮椅上的方政，就站起来了。“人道中国”负责人、原89民运天安门广场学生领袖周锋锁，讲述人们为庆祝方政站起来，在国会大厦举行舞会的动人情景，说道：“方政那天跳舞的时候，现场有140多人，没有一个人不流泪的。这跟20年前，人们对天安门广场上悲壮惨烈的反应是一样的，所有关心中国自由民主事业的人，在这个事情上都受到鼓舞。”<br />
<br />
方政在首都华盛顿附近的马里兰州完成了为期40多天的假肢安装和适应性练习后，回到旧金山，开始了感觉为之一新的生活。出于一腔正义，表达没有忘记20年前中国发生的六四屠杀，以及表达对六四受难者的人道关怀，无偿为方政制作和安装假肢的，是美国奥萨尔智能假肢公司和麦克・科科伦医生。期间方政又得到美国华人基督教组织“对华援助协会”还有当地中国留学生的全力帮助，他们为方政夫妇提供了吃、住、交通和随时随地的翻译服务。方政对记者说：“假肢安装，凝结了美国很多朋友的爱心帮助。通过对我的关心，我感觉到，他们对六四受难者的关心是一直没有减退的。虽然20年了，不管是美国人，还是美国的华人，他们依然没有忘掉我们，依然没有忘掉20年前的事情，这让我感动。”<br />
<br />
中国发生89民运时，方政是北京体育学院的学生。六四凌晨，方政与同学们一起撤离天安门广场，他为了掩护一位女同学，被从后面追上来的解放军坦克碾断了双腿。20年来，六四伤残者方政在一个没有正义的中国，继续遭受政府的摧残、迫害，不断地被国安和警察传讯、监视、抄家、切断电话、限制人身自由，成为他生活的常态。他生路断绝，得不到人们的同情和帮助。他被禁止参加伤残人运动会和任何伤残人的公益活动，他曾经因轮椅损坏在冬夜里北京的马路边无助地坐到天亮。一个残暴的政权、一个冰冷的社会，笼罩着他生活的每一天。<br />
<br />
周锋锁表示，自方政来到美国，一直得到各方面的关心和捐助，他说：“方政一来，我就收到很多电邮，有很多人问候他。有一个朋友说，我没有什么钱，我是作导游的，我希望带他出去转转。前两天我们还收到给方政20块钱的捐款，捐款这个人我知道，他的情况很艰难，他患有绝症，年纪大了，没有收入。像这样的情况，非常多。”<br />
<br />
周锋锁表示：前后为方政站起来出过力的超过100人。方政站起来这件事，其意义不仅仅在于方政本人，他说：“因为六四受难的人非常多，我们做得很少，我们都觉得有亏欠感。就是这种爱心，让各种各样的人把手伸出来。方政这个事情，说明那句话：呼唤良知嘛。中国人的良知还没有泯灭，我们看到在方政这个事情上是闪光的。”<br />
<br />
方政站起来了，今后他可以在美国的土地上自由的舞蹈、自由的迈步走路了。而此时，方正表示：“希望大家更多的不仅仅是关心我，通过我这件事，我想唤起大家，对在国内众多的六四受难者，依然去关注他们、支持他们，他们比我更需要大家的关爱。”</div>

							
							
							
							
