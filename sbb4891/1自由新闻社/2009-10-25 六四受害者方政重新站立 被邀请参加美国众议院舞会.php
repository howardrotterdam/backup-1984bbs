我们的学生队伍前面已经走得很远了，也许前面的队伍都走到西单了。当然我后面还有一些队伍。但是当时，你要知道，从广场撤出的学生队伍，心情悲愤、也有沉痛、也有疲惫，所以大家走得就比较慢，队伍也不是很整齐，不像游行时那样。很松散。一部分走在自行车道，一部分走在人行道上。在马路的南侧，因为拐过来就是在南侧，这我记得很清楚。我们转过来以后就是面朝西，往西走。我感觉我们当时转过来没走多久，我刚面朝西没走多久，身后就传来了爆炸物的声音。<br />
<br />
CND ：身后……那就是天安门广场那个方向。<br />
方政：对，绝对是身后。我记得很清楚，是东面。传来那种爆炸声。坦克也是从东面来的。但是最先出来不是坦克，是那种爆炸物，“咚咚咚”的，是毒气弹。一开始我们不知道是什么东西，就是响，爆炸。然后正好在我们的身边就炸了一颗。一炸就是一团浓烟笼罩。我们也没有经验，不知道是怎么回事。我当时跟那个女孩从广场就一直在一起走。我们当时走在自行车道上，属于马路了。<br />
<br />
CND：所以那个坦克它是逆行的？你们在马路南面，它从背后来，就是逆行啦。<br />
方政：当然从交通规则来说它是逆行了。那我也是逆行。走在马路北侧就不是逆行，靠右行走嘛。南侧就是逆行。当时我们的感觉是整个长安街是很空旷的，根本没有车辆，人也很少。当然路边有一些市民在跟我们讲，哪个车被烧了、哪个地方有人被打伤了，有人在跟我们哭诉。而且我记得我们从天安门广场刚出来的时候还碰到了一队荷枪实弹的士兵，迎面从我们的身边小跑过去，学生还冲着他们喊口号，但是并没有发生冲突，这一队士兵也没有对我们构成威胁。当然路边是一片狼藉啦，可是我当时走到那感觉是很和平，很平静。没有什么人，也没有很混乱，就是学生队伍走在那。<br />
走到那大概有6点钟了，天也有点亮了。一颗毒气弹在我们身边爆炸，当时我一蒙，和我一起的那个女同学更是蒙了。她就晕了。我当时第一反应就是咱俩赶快一块往路边转移吧，往人行道走。但是人行道和自行车道之间有一道栏杆，挺高的呢，得一米多，铁栏杆。那不就挡住了吗？这样一来，当时我们转移我本来是面朝西的，现在我就变成面朝南了。我抱着她就往路边转移。就在这个过程中，因为我面朝南，眼睛的余光就看到坦克从东向西过来。很快的，一下就到了跟前。等我发现它的时候，因为有浓烟嘛，等我发现时坦克已经在头顶了那种感觉，很近的，而且速度比较快。当时没有多想，就把她往那铁栏杆那推。因为人行道过不去，就尽量把她往铁栏杆近处靠一点。上了马路牙子，离着路不就远一点吗？我自己人还站在自行车道上。把她推靠栏杆以后，我自己感觉就是那个坦克炮筒子已经压在我头上，迫在眉睫的那种感觉。我就倒在地下。也没法逃了，因为侧着身子，往前是那个女生，你又不能往后逃，你横着身子这样的角度，侧着身没法跑啊。要转身又来不及了。当时我就势就倒在地上。也不知道当时我为什么选择了这个动作，好像就是一种本能，首先把她往那边一推一靠，然后自己就倒在地上。有点像卧倒似的吧。也可能是被这个炮筒子压迫的。这种惊吓，加上毒气的作用，说老实话我自己也有点慌乱。反正我觉得是个很短暂的瞬间。现在说得慢，但当时也许就是几秒钟的时间，两三秒的时间，很快，连贯性的。<br />
然后就感觉到自己一种被挤压的感觉。那时候头脑还清醒着，我就意识到，坏了，压着了！但不知道压到哪。紧跟着就觉得自己被拖行，在马路上，这个还有意识的。在路上拖行，估计是裤子或者哪里被坦克的履带绞住了，拖行。咚咚咚的一种颠簸的感觉。那时候头脑还是清醒的。拖行以后自己“咚”掉到地下，这也清醒，也还是知道的。那时候还没有想到疼不疼，然后自己本能一样的就滚到路边，靠到那个铁栏杆了――因为我们当时就在铁栏杆边上。所以后来有一个照片就是真实的记录，和我当时失去记忆前的那种感觉特别能吻合。所以我特别感激这个照相者，也想找到当时在给我抢救、扎血管止血的这个人。我想如果要是这些人能够出来跟我共同回忆那个情节，他们作为旁观者也许会看得更清楚。我自己其实就是在一种慌乱中，对这个遭遇记得并不是很清晰的。当然我靠在栏杆上是有一点感觉的。我最后的意识就是靠在栏杆上，自己能看到自己腿前面露出的白色骨头（CND注：指着右腿），这个是我意识消失前最后的一个记忆片段。<br />
后来我到积水潭医院醒来后，医生问，你醒啦？知道你现在是什么情况吗？我跟医生讲的第一句话就是：“我的脚是不是没了？”因为我记得当时看的时候就没有脚了，就是前面有个骨头。所以我第一句话就是这个。医生也很惊讶，说确实是，你来的时候脚就没了。我们尽力了，就是现在这个样子，他宽慰我。<br />
<br />
CND：你恢复意识是在多长时间以后？<br />
方政：6月5号的上午，大概9、10点钟？超过24小时了。我几点钟到积水潭医院就不清楚了。后来都是医生补述给我的。我最后的记忆就是靠在铁栏杆上。后来我听积水潭的医生说当时有人抢救。说这个人抢救施行得挺得当，止住血了。不然流血过多就死亡了。然后先给我送到离六部口比较近的二隆路医院。可是二隆路医院在长安街周边，伤员特多，而且我这又比较严重，正好那有个救护车，就把我和另外两个重伤的拉到了积水潭医院。另外两个人后来死了，就剩了我后来抢救活了。当时积水潭医院那时候也是人很多。我记得我醒的时候是躺在他们医院会议室的地上，都睡在地板上。是他们临时的抢救措施，都在地板上。所以我周围站的人显得特高，觉得一睁眼就是 “哇！”的这种感觉（CND注：向上方看着），特别虚幻的一种感觉，就觉得特高。旁边有一个护士值班台靠我很近，记得我好像是在前排。医生说这是我们的会议室。当然中间的抢救过程，包括医生说医院的血都用完了，本来我这个手术需要2000毫升，后来凑合用了600，然后过一阵有人献血，我手术完了有危险又补输。这些当然我都不知道了，是在我昏迷状态下发生的。<br />
<br />
CND：你受伤以后又见过那个女同学吗?<br />
方政：当然见过了。在医院她还来过呢。我在医院的时候，也不知道是几号了，她到医院来过。我还问过她，说你怎么样？她说当时毒气中她晕了，昏了。这肯定是事实。然后老百姓把她救到家里去了。然后她从市民家里回的学校。后来她知道我受伤了还到积水潭医院来看过我。<br />
<br />
CND：大家都知道你救的那个女孩子后来不承认你救了她。她是不记得呢，还是……<br />
方政：这个细节是这样的。<br />
<br />
第二部分：二十年的漂泊<br />
方政：这个细节是这样的。我回到学校就没见过她了。她先回学校的。我是6月底回学校的。回去以后就开始清查――清理、清查，双清嘛。学校就开始问了，怎么受伤啦？为什么会被坦克压？问这些细节。我就原原本本地把这个过程讲了。校方就质疑啊，为什么坦克会压你，是不是有什么暴力行为？我说没有啊。校方说那你要证据，要证明你是没有暴力行为，否则就是暴徒。当时不是有一个界定吗？是暴徒还是误伤。我说证人我找不着，只有旁边这一个女孩。其他的人我也不认识，因为体院那天晚上在广场的人很少，我跟他们也不是很熟。其他学校我就不认识了，虽然当时周围很多人。但是我说我只有跟她。我说你问她，最起码她能知道，毒气来之前咱们一块好好的从广场怎么和平地撤，跟我走在一块手拉手――因为她很害怕。然后后来到毒气来了，她躺在路上，别人救走。我说她自己的经历就足以证明当时的情况了，对吧？后来校方告诉我说，她（停顿）……她否认这个事实。她说她不知道。<br />
<br />
CND：否认哪个事实？<br />
方政：否认跟我在一起的事实。当时我也很惊讶……说这不会呀，怎么会这样呢？校方就说她不能给你证明什么。那就不能界定我是误伤。因为当时我们认为我们绝对是误伤。当然现在看来也未必是误伤了，对吧？他可能就是蓄意的伤害。但是当时就是要界定误伤和暴徒。要定为误伤是因为我们要表明我们没有所谓暴乱的倾向，没有暴力行为。我们是无辜的。完全无辜的，对吧？我当时就不懂为什么不能证明我是误伤呢？校方就是要证人。后来北京钢铁学院的两个老师，他们也知道，因为他们也经历了六部口。后来知道的，在这个路段，青年政治学院的伤亡比较多，北京钢铁学院的伤亡也比较多。就是那几个学校走在这一个路段。所以那两个老师说，我们就走在人行道上，毒气也把我熏晕了。当时看着坦克从学生队伍中间冲压过去，压倒人。他们都是目击者。他们说我们来给你作证。他们就到我们学校去找校党委去作证。但是学校党委后来也没采纳这个证言，也没效。反正最后校方也就不了了之，他也不能界定我是暴徒，他更不愿意界定我是误伤。后来还有个小细节。学校的领导有人劝我，问我是不是坦克压的。我说是啊。他说那你是不是看清楚了？我说当然看清楚了。就是坦克啊，没问题的。我每天不是都要写这个经过吗？他不要我写是坦克压的。其实平时在学校都是关系很好的老师、领导，比如我们系的支部书记啊，还有学校的领导，我作为学生干部跟他们打交道也很多，都是一些我平时很敬重的老师。但是也许他们内心里也接受不了这是坦克，尽管他们自认为自己是一个坚定的共产党员或者是一个爱国的人，但是他们内心也不愿意接受这是坦克。不知道是出于什么原因，也许是“坦克”这个字眼有点刺激。他说你写成“军车”、写成“装甲车”都行，别写坦克压的。我说那不行，我得尊重事实。如果我自己经历的我都胡写，这是对自己不负责任。我说坦克就是坦克。但是至于坦克为什么要压我，那时候我也没办法知道。我只能把我所经历的原本地告诉你们，你们怎么去界定那是你们的事。所以我还是坚持这一点。当时我作为党员要接受更多的“思想教育”，但是我觉得那些都不是很重要。我必须首先保证自己的真实性。而且这种真实性我在积水潭医院的时候就是这样了。在积水潭医院的时候，当时我还躺在床上。中间有两个传闻。不记得是哪一天，突然很紧张，医生就推着我到处藏。那时候我已经醒来，我说怎么回事啊？他说部队要到医院来抓人，要军管医院，要来搜查学生。那护士真的很好。她把我推到电梯井的地下室，藏在那。后来又说是谣言，不是军队来。就是北京市公安局西城分局的要到医院里来，对每一个人做调查，要做笔录，要问询。好，大家松一口气，说，好，不是要抓我们。医生就把我推到一个办公室，我还在吊着点滴呢，躺在床上。他们说我们是北京市公安局西城分局的，我们要做调查。那我也是原原本本地把这些情况告诉他们。记得后来还要按手印。所以那时候官方――所谓的官方就是公安系统――已经做过调查了。当然到学校就是另外的一种调查，重新来审查。也许其他人都经历了这个过程。<br />
<br />
CND：你说你是6月底回学校的？<br />
方政：我是6月24号离开积水潭医院的。住了20天，那时候基本好了，但是这个腿（指着右腿）还不行。<br />
<br />
CND：这个腿的膝盖还在。<br />
方政：留了一点。记得我那时在积水潭遇到了一个老专家，那个医生就跟我讲，这个腿（指着左腿）当时从膝盖以上就压掉了。老大夫说，小伙子你不知道，你很幸运遇到我了。我记得那个老专家好像是姓杨。他说今天我在这，我想尽了办法把你这个膝关节保留下来了。这是极限位置，给你保留了。以后你就会知道，有这个膝关节，你要是做假肢什么的，对你有很大的帮助。这是他原原本本的话。他说我们真的尽力了。这个腿（左腿）没办法给你保留了。这个右腿按道理我们也基本保留不住了，因为它太短了。而且髌韧带他都给我重新加固了，你看这有个印，钉的东西嘛。我们学过生理的都知道，膝关节的屈伸是要靠这个髌韧带的。这是个大韧带，是带动膝关节屈伸的。他说你这个髌韧带已经没有附着点了，他做了比较复杂的手术，给我加固了。所以留了这个。你看我这个膝关节下大概有个4、5公分，是吧？他说就是你留这一点点，以后做假肢就比左腿就要好多了。当时我躺在床上晕晕乎乎的，还没想到装假肢的事，那还是很遥远的事。那时候还是危险期呢。但是我就记住他这句话了。一个胖胖的、头发都白了的老专家，好像是姓杨。我希望以后能够去感谢他。这是印象比较深的在积水潭医院的细节吧。现在证明了他的话。我今天到装假肢的地方去问，他们就说，哎呀，你有膝关节，很好。确实也起了很大帮助。<br />
<br />
CND：你是什么时候离开学校的？<br />
方政：我是6月24号从积水潭医院回学校的。当时这个伤口还没有完全好。我们学校有校医院。就住在校医院继续治疗，又拖了有好几个月。然后一直就住在学校医院，大概住了有大半年。校医院也是个经营单位嘛，觉得我好了，住在那也不方便，病人也多，互相也有干扰。后来就在我们学校操场后面，体育场有一个体育器材室，废弃的。他们就腾出来一间让我住。平时也没人，只有上课的时候有人来拿一点体育器材。给了我这个小房间。我就住在那，一直住到92年的3月。等于我在北京体育学院一直待到92年3月才离开。<br />
<br />
CND：这期间主要是治疗吗？<br />
方政：其实都好了。治疗大概是又过了一两个月就好了。然后就是身体康复。<br />
<br />
CND：你当全国冠军是在体育学院的时候吗？<br />
方政：是这样。在身体康复过程中，因为我们学校有很多老师，很好。而且他们就学运动生理啊、运动康复保健啊，他就说你疗伤还要进一步的锻炼身体，会对你的康复有好处。所以我就做一些康复训练。在这个过程中，大概是91年初，认识了一个北京的一个大姐。北京马家堡那有一个中国残疾人康复中心，她是那的工作人员。她也很关心我。大姐的丈夫也是个残疾人，不仅是个残疾人，而且是一个优秀的残疾运动员。这位大姐知道我的情况了。她做的工作和我们运动力学是很接近的，也是搞人体康复的。她说，你也可以搞残疾人体育。尽管我在上体育大学，但我们是正常人，以前我们并不知道残疾人体育这个领域。后来我见到她，她就跟我讲了很多。讲了她的丈夫，失去了一条腿，后来游泳、搞残疾人训练，都很好。我说，哎，那我也可以从事啊！选个什么项目呢？游泳我也会，但是游泳会很麻烦，要到体育馆去，没人帮我，不好。后来我说，我一个人练，因为我坐在轮椅上。她说，就练练田径项目吧，坐在轮椅上练练投掷――铁饼、标枪。虽然我以前不是练这个的，但是比普通残疾人我还是显得有点优势的，学得也快。然后大姐告诉我说北京市要组建一个残疾人运动队，要参加92年在广州的全国残疾人运动会。北京市残联一个工作人员看到我说，挺好挺好！北京市肯定需要你这样的人才。然后这样我在学校凑合着练练，92年的3月就代表北京，参加代表团了。我记得北京市副市长何鲁丽还是我们代表团团长呢。去了广州全国残疾人运动会，我参加的这两个项目我都是第一名。所以这全国冠军就是这么来的。（笑）当然在这运动会之前，学校基本上就不愿意我在学校待了。而且我自己也待不下去了，因为也没工作，也没收入。那时我妹妹在照顾我。她在北京一个公司里打工，中关村那时刚有电脑公司。她打字快，给人家当电脑录入员，有点收入。我呢，因为离开学校之前毕业证还没有给我，就还按照学生的待遇，一个月给我36块钱的伙食费，是饭菜票。凑合，也算是一个补助。平时我要是没什么事呢，要不就是给出版社校对稿子啊，要不就是有些老师做实验什么的，我就帮点忙。老师们照顾我，给我一点费用。就这样待到92年，学校说不能待了。我当时考虑年龄也不小了。下一步谋生该怎么办呢？<br />
<br />
CND：广州华南师范大学那边肯定是不行了？<br />
方政：不行了。当时我受伤之后还听说，华南师大给我们学校来过3次电报，催我怎么不去报到。校方就把他回了，说该同学因为意外不能前往报到了，就改派我们班另外一个同学去。华南师大就没有要他。后来等了两年之后，87级有一个同学，也是我们这个专业的，毕业以后分到华南师大去了。<br />
<br />
CND：这20年里，除了在学校这段时间以外，你谋生靠的是什么？<br />
方政：3月份在广州的全国残疾人运动会上拿了冠军以后，我就没有直接回北京。我从广州到了海口。因为这之前在海南有一个同乡大姐，她很照顾我。她的先生原来是北大经济学的讲师，后来下海了，在海南开发房地产。大姐说你到我们海南来，这里的经济形势好，谋生容易一些，机会多一点，另外政治上也不像北京那样管得很死，挺左、挺压抑的。而且回合肥我也没地方待。家里那时候住在4楼，上下楼也没办法啊。也没地方待，也没工作，回去干嘛呢？所以我记得是92年3月24号，我就从广州到了海口。在大姐先生的公司里，他们开发了一片房地产，别墅。我就过去帮着做房地产的销售和物业管理。那个时候我以前的女朋友也跟我一道去了。<br />
<br />
转发二：<br />
&lt;&lt;CND&gt;&gt;专访方政(三)BY 吴放（来自《方政的20年》）<br />
追忆往事的方政<br />
CND：她是什么样的一个女孩子？描述一下你心里的你的太太吧。<br />
<br />
方政：就讲真人吧，就现在这样。她叫朱进，以前也练过体育。她比我小将近10岁，是76年的，那时候22岁。我记得是98年底，她大专毕业，也要找工作，因为不包分配了。她在海口这边有朋友先来了，说你到海南来找工作吧。她就来了。但这里也不是她想像的那样，可能工作也很难找，她也没有很强的专业能力，也没什么钱。所以几个女孩一起租了我画家朋友别墅里的一个房间。就是这样认识的。<br />
<br />
CND：太太的个性是什么样？<br />
<br />
方政：她比较有爱心。她以前当过运动员，所以她的个性属于比较大方、比较豪爽，身体也结实。当时有个细节就是住别墅不方便，一楼都是客厅，吃饭用的，睡觉都在2楼、3楼。我有的时候上不去，她就说，我来背你！因为经常是我那个朋友背我。他有的时候忙、不在家，我有的时候在楼下就上不去。她就说，我来背，我来背！我说哎哟，你还可以啊！她说，你不知道，我以前练过体育，背你没问题。所以她比较有爱心，对我帮助也比较大。<br />
<br />
后来因为我不能走啦，又得在海南待下去啦。那时候我身体不好，老咳嗽生病，她就对我比较关照。我就觉得这个女孩真的不错，有一种感觉了。后来因为住别墅不是很方便，我就另外租了一个带电梯的房子。她们那两三个女孩说，干脆我们跟你一块合租得了。我说那更好啊，分担我一点房租，然后回家还有个照应。就这样我们就更进一步了。什么时候开始跟她一对一的我也弄不清楚，反正慢慢就觉得我跟她更有共同语言，更异样一点了。<br />
<br />
CND：一般的人都会想，要是嫁一个残疾人的话，生活不便是一个很现实的问题。你太太从来没有因为这些有过犹豫、有过抱怨、有过遗憾吗？<br />
<br />
方政：可能在海南那个时候，虽然我是残疾人，大家却都有一种“同是天涯沦落人”的感觉，也就是说，大家的境况都不是很好。所以那个时候就削减了这种所谓的残疾不残疾。而且我的生活状态，给她的感觉倒不是太觉得残疾人有什么了不得的困难。我不知道她怎么想的。反正我就是有什么事我也干，家里做饭炒菜，她要是不方便时我来弄。出去买菜，我推个轮椅出去可能比她还快。我说海南的太阳太晒，女孩子又怕晒，出门又麻烦，我就推轮椅跑出去买。就这样。<br />
<br />
另外她可能觉得我比她懂得多。虽然她是念体育的，可我是学体育的，比她还高一层吧，对不对？（笑）所以她也许还有一点崇拜的感觉。她挺有爱心的，也比较单纯。她不像有一些女孩子可能会有世俗的一些想法，会过多考虑一些物质的东西。那个时候我们什么都没有，在海南也是睡地铺的，很简单的。<br />
<br />
CND：但是你不光是残疾人，有身体残疾的问题。你还有很多政治包袱要背着。你是什么时候告诉她的？<br />
<br />
方政：我是一点一点告诉她的。说老实话，她那个年龄，对6&#8226;4也很淡。<br />
<br />
CND：她那时还不到10岁吧？<br />
<br />
方政：13岁了。她就说什么，解放军好多人被你们杀死了。她还有这种想法，因为当时有很多宣传就是这样。我就跟她讲了很多，她慢慢才知道一点。但是，首先她肯定是认为我还是一个很值得信赖的好人。她肯定是先从心里接受我这个人，然后再附加的这些――不管是残疾也好，政治包袱也好――她才会考虑。但是也许那些对她来说，她都认为不是很重要的。先认可了一个人之后，那个人身上附带的这些所谓的背景，她就考虑得不是太多了。<br />
<br />
CND： 你们什么时候结婚的？<br />
<br />
方政：我们正式结婚是2000年9月。中间有个插曲。99年3月我不是被抓回来了吗？到99年底，有一次她妈妈叫她回家。我去送她。她也是坐船走，坐飞机比较贵嘛。我送她到海口的港口，坐轮渡过海。轮渡的人看我这个样子，就说，你送她，没事，你可以上我这个轮渡，送她过海。然后我们的船回来再把你捎回来，不要你买票。我说好啊，坐船玩一圈吧。快船一个小时就过了琼州海峡。在船上我就说，唉，在海南这么多年我都没回过家，就讲起来3月份被抓回来的经过。她就说，哎，这次，走，跟我一块回去啊？咱就过海，咱就不回去。看看，抓回来再说，不抓回来，没准可以呢。我说，也行，反正我也无牵无挂的，家里什么也没有。和我一起送她的一个女孩也劝我，说我们一块还可以照顾你，免得你一个人想回去时更麻烦。<br />
<br />
CND：她的家在哪？<br />
<br />
方政：她家是江苏徐州。她说我们徐州离合肥也不是很远。我说，那好，咱们再走一次，也许就没人管我了。就这样，因为送她，突发奇想，就坐这个船过了海，然后坐汽车到广州，坐火车到徐州。先去她家。一路上很顺，就到徐州了，也没事。我想，是不是3月份的时候又是两会、又是6&#8226;4十周年，他那次抓我。现在过了敏感时期，就没事了。我也不怕他抓我，我有过经验了，顶多把我带回海口。这样就到了徐州，作为一般朋友我跟她母亲见了一面。然后紧跟着我和她就从徐州到了合肥。一到合肥，我家里觉得是挺重大一个事。我这么多年没回来，一回来还带来一个女孩，挺细心照顾我的。就觉得挺高兴。不是我以前的女朋友也分手了吗？家里也知道，我在海南就是一个人混，也不是个事，都不小了，30多了嘛。他们就说，订婚吧。朱进也没反对，说好，那就订婚。于是在合肥我们家几个亲戚朋友就一块聚了一下，就算订了婚。在家待了有10来天，我们又回海口了。一路畅通无阻。<br />
<br />
那个时候就开始考虑结婚的问题了。一考虑结婚，我就出现了一个重大问题――我没有户口。我就要赶快想办法，给公安部写信，让我妹妹也到公安部去找。当年93年的时候我离开北京，他们给了我一个“户口迁出证明”，从大学生集体户口迁出了。按道理户口会跟着档案，有工作到哪里再落户口。可是我到了海口，咱也没有正式工作，海口就不让我落，说我不符合落户海口的条件。这样就是一张迁出纸，海口落不上，北京又不让回了。那张纸就一直拿在手里。那时候没有什么紧迫感，没户口就这样过着吧。后来一说要结婚、生孩子，这是挺麻烦的一件事，没户口不行。我赶快给公安部写了一封信，说你随便把我的户口落到哪，北京、海口、合肥以及中国任何其他地方。作为中华人民共和国公民，我希望有一个合法身份。不然就没我这个人了，对吧？最后到2000年5月份，我家人说，户口落到父母身边了。说这可能是一个最合理的办法。他们给我办了一个身份证，寄给我了。2000年8月份，我对朱进说，咱们回家吧。<br />
<br />
我们先去了北京一趟，然后从北京跟我妹妹一块回了安徽合肥。然后，我记得是9月8号，就正式结婚了。后来发现她怀孕了，我妈不让她走，说你就在这吧，生完孩子再说。本来我们还没有说要离开海南、不回海口的。后来一在合肥待下来又觉得，那时候海口也没什么可牵挂的，也没什么东西了，就甭回去了。2001年5月5号，我女儿出生了。就这样，从2000年就彻底离开了海南，到了合肥，跟父母生活在一起。一直到我这次离开中国，都是在安徽合肥。<br />
<br />
CND：这期间你做什么呢？<br />
<br />
方政：几个过程吧。1992年之前是在学校，比较简单。1992年到2000年是在海南，这个时间比较长。刚开始是做房地产销售和物业管理。紧跟着没多长时间，海南房地产泡沫崩溃了，照顾我的这位大姐的先生公司的情况也不太好。所以我就自己出来，在工地旁边开个小商店，小便利店。然后后来那边整治路，工头就利用工地整修，商店就开不了了。大概是1995、96年，我又跟朋友合伙开过一个小旅游公司，干了有两、三年。后来整顿旅游市场，我们没有保证金――要交几十万的保证金的――后来遇到些波折，又结束了。为什么认识这个画家朋友呢？是因为跟朋友还搞过一个画廊，艺术交流中心，干过一段时间，所以认识了画家。中途还和朋友合伙开过一个火锅店吧，我记得。也都是不成功，反正咱也不是经商的料（笑）。但是为了谋生做过很多事。<br />
<br />
CND：回到合肥以后呢？做什么？<br />
<br />
方政：回到合肥，生了孩子，2001年以后我跟我太太很着急。老跟父母在一起当然是衣食无忧啦，生活、吃饭倒是没什么问题，父母都有退休金。但我俩也着急要干个事。后来我太太就说，我想开个店，就是那种化妆品啊、美容的那种店。我说，也行啊，我也可以帮你。正好有一个朋友帮忙。说来还要感谢她，我受伤以后这么多年，这个朋友，一个美国的老大姐――不知道她愿不愿意我把她的名字说出来――是姓杨的一个女士。她是通过网上的民主论坛给了我们折合人民币大约3万块钱的帮助，说希望我们做个小生意谋生。这是给我们的唯一的一笔比较大的资助。<br />
<br />
在这中间也有一些其他的帮助。比如说丁子霖老师她们，天安门母亲，跟我联系上之后，后来的这几年大概每年给过我2000到4000元人民币不等的支持。这是我在国内明确得到的帮助。所以我很感激。官方当然没有任何表示。既然你问到20年，我觉得说出来对他们也不会有什么坏的影响。在20年当中，其实也有很多朋友的关爱，也间接得到过海外朋友少许的帮助。一个姓张的女士，也是美国的。不知道从哪里知道我的地址，曾经还给我写过一封信，好像还给我寄过一点美元。都是很有爱心的人。在国内那么艰苦的环境里，让我感到挺温暖的，有人支持我。尤其象丁老师、天安门母亲。她们虽然都是死难者家属，但是也把我们这几个伤残的人员纳入了她们天安门母亲的群体。比如说每年有一些公开信啊、呼吁、签名，我们都是在其中之列的。而且在生活上给予我一定的照顾，很不容易，这么多年一直在坚持着。我觉得丁老师做这件事，并不是只包括我一个人，还有很多难属、很多伤残人，或多或少她都尽力支持。我想她给我的是最多的。<br />
<br />
CND ：咱们现在转一下话题，说一说你到美国来以后有什么打算，还有现在的安排是什么样的。大家都非常关心你现在的情况和以后的计划。<br />
<br />
方政：对，这就是一个全新的生活展开了。在国内来之前呢，有一个2008年奥运会。08年这个时候，官方――我们当地的政府――也积极地做了很多工作。不知道出于什么样的想法。也许是希望08年奥运会的时候要稳定吧，是出于担忧；也许是他知道我有出国的想法。我记得当时地方的政府说，你要是答应不出国，我们会给你什么样的安排等等。当时我就跟他们说，我不怀疑你们地方公安或者残联的这种真诚，但是我的问题是，第一，你这个来得太迟了，将近20年了都过去了。在这之前你们没有给过任何帮助。第二，你们这也不是最终的、来自中国政府的一个解决方案。我的感觉，这不是很稳定的。你们的承诺能代表中国政府的意见吗？会有多久？而且会不会有变化？你不能让我有放心的感觉。<br />
<br />
我记得那时候我拿了护照想出国之前，残联系统、公安系统的人都找过我很多次。刚开始是劝我不要出国，后来我说我还是想出国看看，这么多年了，对吧？再一个呢，我想出国做一个好一点的假肢，对我的生活和工作都会有更大的帮助。他们又说，那你就等2009年7月份再出国，6&#8226;4以后再出国――就讲明了。我就笑笑说，这是个特殊时期是吧？过了6&#8226;4是吧？他说，是啊，你也知道你自己的情况。我说，我不好答应你。因为出国是海外的朋友在帮忙，机票都是他们买的。时间当然要由他们来定。而且对于我来说，什么时间不是很重要。所以，我说，可能上半年2、3月份就要出来。后来他们就说，你一定要坚持出国，我们不一定会强行阻拦你。但是你要考虑后果，我们也许就不会再让你回来了。这是当时公安明确跟我讲的谈话内容。<br />
<br />
当然最后我还是顺利地拿了护照，我是在奥运会期间拿到的护照。然后就出国了。<br />
<br />
CND：你现在是在加州那边吧？<br />
<br />
方政：对。我这次出国主要是旧金山那周锋锁他们的“人道中国”给我的帮助。他们给我发邀请，然后第一站到那里，前期的安顿都给了我很大的帮助。<br />
<br />
CND：你在美国有什么计划呢？要做假肢，买轮椅，还有什么？<br />
<br />
方政：轮椅的问题这次来华盛顿就可以解决了。这边有一个美国的热心朋友说帮我弄了一个轮椅。轮椅其实倒不是大问题。我现在带了一个轮椅，也凑合能用。尽管破烂一点，但还能用。这里轮椅店很多，很容易找到适合我的轮椅。而且我不想要电动轮椅。我自己推，对身体也有好处。等到以后我推不动了再换电动的吧。主要就是假肢。在美国装一个好一点的假肢费用是很高的。现在还没有完全定下来，但是肯定是可以装的。费用方面，一些朋友还在寻找美国一些机构的资助。因为如果靠筹措，就要靠海外朋友个人来贡献，对大家也是一个很大的压力。如果能够得到机构的资助当然是最好的了。<br />
<br />
CND：好的假肢大概需要多少费用？<br />
<br />
方政：大概要5万美元左右吧。<br />
<br />
CND：是装到身上就可以走路了吗？<br />
<br />
方政：也不是那么简单。当然要自己适应，它毕竟是个身外之物。它的好就好在质量，比如说稳定性会好一点，或者人穿上不是很难受，适应性会好一点。最主要的还是自己要去努力地跟它训练、磨合。这个过一段时间就会知道了。<br />
<br />
CND：你们全家是移民吗？<br />
<br />
方政：我这次来拿的是旅游签证。但是我想寻求在这里获得移民的身份。还没开始办。我是想留在美国。我来到美国之后的感受就是，我觉得这还是挺好的，自由的天地。另外很多朋友也说，不要担心，在美国如果你不是追求很高，作为一个简单的生活是没有问题的。我们就是很平常的，包括我太太，就是要追求一个很平静很简单的生活，主要是考虑孩子能够有一个很好的生长环境和教育环境。这就行了，就够了。我们不会去追求多么高的生活水平，从来没想过。所以朋友们都说，如果你们这样想，应该是没问题的，在美国肯定能生活下去。所以我就想，那行。先做第一步的打算，准备长期留在美国生活。<br />
<br />
说老实话，这三个多月，赶到这段特殊的时间来，是因为有这种责任和使命，要参与各种纪念活动。我觉得我作为一个亲身经历者，也是受害者。我们要不说，那谁说呢？对不对？怎么叫纪念6&#8226;4？第一步就是要把真相告诉大家。然后让大家在这个真相的基础上去做他自己合理的判断。所以一直到现在才刚刚相对比较稳定下来。前一段时间我们在旧金山跟朋友合住在一起，跟这个家里合住、跟那个家里合住，还没有完完整整地自己作为一个家庭开始生活。我想这下一步是我和我太太要面对的。我们自己要开始生活。我们要去租房子，要去买菜，要送孩子上学，要过日子。现在是刚刚开始。<br />
<br />
CND：这一天肯定很快就会到了。<br />
<br />
方政：很快，（笑）马上就要有。因为你不能老跟别人生活在一起呀，不是个长久之计。我太太的心态很好。她说，没问题。她说，什么工作她都可以做，从最基本的做起。她来后这段时间我们要是没什么事，或者我要是有事，她在家，她还给别人在家里做过几天保姆，还挣了几百块钱呢（笑）！她还给人去打扫卫生，这也干。她受的教育也许不是很多。我们两个都有一个共同的问题就是语言都不能过关。当然在加州语言的压力不是特大。她要是在华人圈里找工作倒是能挣一点钱养家的。<br />
<br />
对于我来说我就不能依赖这个。首先我想，一个家里，尤其对孩子的成长来说，必须要有一个人要接触这个美国社会。那么首先语言必须要过关。所以我想我今后这1、2年甚至2、3年的时间，首先要做的就是去学习。在学语言的同时也许我还会去学其他的，这是一个相互促进的过程，也不是单纯地就是学语言。也许在学习的过程中同时语言也会有所提高。<br />
<br />
我女儿来上2年级，下学期也许上3年级。她8周岁了。不管她上2年级还是3年级，反正我跟着她一块进步吧。（笑）没准3年以后，她上到5年级或者6年级了，那我们大家都会有进步的。所以我们的当务之急是学习。<br />
<br />
除了学语言，既然来到了美国，我就要了解美国社会。就要学一些在美国相关的一些专业性的东西。有机会的话我也想去学习。<br />
<br />
CND：你今年多大年纪了？<br />
<br />
方政：我42周岁多，快43了。<br />
<br />
CND：完全来得及的。<br />
<br />
方政：来得及！（笑）但是我现在心虚，不知道能不能学下来。我是这样想：也许我学个3年5年的，不一定能够拿到什么博士、硕士学位。但是我想，在学的过程中我已经掌握了很多。我不是追求什么学位，我也不想去做很专业的人。比如说我去学法律。我可能学了3年5年，很艰苦。我就要去学它。学了5年我未必能够拿到一个法学的博士硕士，但是我想通过学习，我会有一定的积累。这就够了。这为我以后再去关注中国，关注与我们相关的事情是会有很大的帮助的。我就是这样想。我就想在学一个专业的同时去丰富自己。我不想就拿着一本英语书去学语言。结合专业去学，慢慢的呢，就像蚂蚁搬家一样，我通过几年的学习，会有进步的。毕竟才40多一点。好像还行，我觉得我的精力还能跟得上。<br />
<br />
另外如果有机会我再继续练练体育，对身体也是一种帮助。也许可以代表美国去参加残奥会（笑）。有些项目我还是做得不错的。比如说射箭，我在国内练过，轮椅上坐着可以射箭，轮椅上打乒乓球，还有轮椅篮球等等等等。这些我都可以。当然作为一种爱好，作为我生活的一个补充，我也很愿意从事体育活动。这样可能我的精神状态也会好一点，生活也会积极一点。反正就是这么简单的想法。（笑）<br />
<br />
我跟我太太的想法就是这样。可能家庭中的重担她会担得多一点。工作方面，她要挣钱养家嘛。不管怎么样，她要去打工，又要照顾小孩上学放学。如果我以后便利了，我要有个自己能开的车，像我们这种残疾人有全手动的汽车。这样我在学习的同时就可以把接送孩子啊这些事情由我来做。这个家就不需要太多人帮助了。<br />
<br />
CND：讲讲你女儿的小故事。<br />
<br />
方政：因为我们跟父母生活在一起。所以女儿小的时候，我的父母很照看她。我呢？因为不方便，可能就管得少一点。我太太有时候要出去工作，所以我父母照看得很多。但是我们从小都一直生活在一起。她的个子长得比较高，从小比同龄孩子的个子就高，现在都有1米4多了，8岁。<br />
<br />
<br />
<br />
方政与女儿<br />
<br />
CND：是随你吧？你1米8。<br />
<br />
方政：我太太才高，1米74呢。应该是随她，别人都讲，更像她。女儿比别的小孩好像更懂事一点。但是有的时候我感觉，看到我的腿，她会有一些伤感。女儿的心细嘛。当然我和我太太经常要淡化孩子这种心情，尽量让孩子感觉到，我们是正常的家庭，爸爸妈妈都是正常人一样，没有任何问题。唯一的差异就是人家能拉着孩子走路送他上学，我们就推轮椅送你上学。其实我们是刻意在做淡化孩子这种心情的工作。我们从言语中、行动上从来不去强化她这种感觉。但是好像也不行。毕竟孩子有她自己的眼睛和思维。我感觉她多多少少心里面有一点这种薄薄的阴影，像一张薄纱一样，盖在她的心里。所以这也是我们很担忧的事情。<br />
<br />
女儿成熟得早，对我也特别的好。从情感上特亲近。有几个小细节。比如说，我的轮椅放在那，不给别人坐。有朋友到家里来，要坐轮椅。我家女儿在一旁就会生气，一定要把轮椅拉开，不让别人碰我的东西。爸爸的轮椅。（笑）然后比方我要出去，有的人喜欢推我。她就不让推。她说，你不要推他，我爸爸自己推，要不就我来推。她有一种要关爱、要保护我的需求。她内心里从小就有这个。现在8岁，已经表现出来了。<br />
<br />
CND：好女儿啊！她有什么爱好、特长吗？<br />
<br />
方政：她弹琴还可以。在国内因为买不起钢琴，所以是电子琴。让她弹一弹，一个星期到少年宫去学一次。老师说她还可以。因为我也不会，我太太也不会。她就自己去。我在国内有一个残疾人三轮摩托车。我就把她带上，开到少年宫。然后她自己上去，我就在底下等她，一个小时或半个小时。她学完了，回来就自己弹。我不知道她弹得怎么样，因为我从来没有上楼去听过。有很多家长可以跟着孩子一块去听，去指导。我只能给老师打电话。老师每次总是说，还不错。她自己每天抽一个小时半个小时自己练一练。我感觉还可以。老师说，不错啊。你们从来不指导她，也没管她，她自己能这样，已经很好了。<br />
<br />
另外她也挺好动，可能遗传了一点体育的基因。尽管她比较瘦，但是我觉得她运动能力比人家强。她跑起来、蹦起来还挺欢快。当然我们也没有刻意地让她去练。<br />
<br />
CND：她学习怎么样？<br />
<br />
方政：她在国内只上了一个完整的1年级。2年级下学期就到这里来了。1年级在他们班学习肯定是不错的。现在所有的汉文书籍、报纸都能看来。认字基本上都会了。其他的还都不知道，才上2年级，很难说。（笑）反正不笨，还可以。（笑）智力是正常的。<br />
<br />
女儿在美国上了几天学。她就说，哟，这怎么上学跟玩一样啊！她那个时候在国内上的是合肥科大附小。她说我们那时候写作业，天天写，多得不得了。手指头都写变形了。我告诉她，这就是这里的学习方式。但是我也告诉她，你不能一味地玩。老师给你们轻松玩的东西你要用心。她还在学英语。等我们安顿下来，找个学校，然后再给她找些同伴。<br />
<br />
CND：这次纪念6&#8226;4的时候我们看到了许多的孩子。你想对你自己的孩子说什么？<br />
<br />
方政：其实我女儿在国内对我们的事情就有所了解。尽管她的年龄不大，但她是知道的。另外，不管怎么样她还是中国人嘛。对中国的历史、中国发生的事情，我希望她多少要了解一点。也许她要长期在美国，受美国的教育，但我不希望她的汉语会丢失。要让孩子了解父辈的经历。这样她对父辈的理解和沟通方面就更容易一些。要薪火相传。所以我参加很多活动都带着她一道去。我也不会刻意对她灌输什么，因为她还小。但是让她在参加当中去体验，让她用自己的眼睛去看。等到她长大了，随着她的见识增加，她能够做出自己的判断。</div>

							
							
							
							
