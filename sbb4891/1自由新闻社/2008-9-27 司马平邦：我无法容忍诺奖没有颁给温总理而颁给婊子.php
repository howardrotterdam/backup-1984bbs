													<h2>有趣的五毛--司马平邦</h2>
						
						
																											<div id="postmessage_82398" class="t_msgfont">今年（08）的和平奖是诺贝尔和婊子养的<br />
<br />
&nbsp; &nbsp; 司马平邦<br />
<br />
&nbsp; &nbsp; 让被中国政府判了刑的罪犯得到2008年的诺贝尔和平奖，如果这事确实DD连中国的外交部发言人都出来表态，证明这个传言是有谱的DD我就可以说，今年的诺贝尔和平奖，是诺贝尔和婊子养的。<br />
<br />
　　虽然这对死去100多年的阿尔弗雷德・贝恩哈德・诺贝尔先生有点儿不恭。<br />
<br />
　　我是说诺贝尔奖委员会。<br />
<br />
　　对于胡佳，我不多置评，不了解。*<br />
<br />
　　但我更关心诺贝尔奖委员会向一个被中国政府判刑的人授奖的那种阴暗心理，不是正常的“人娘养的”心理，而有点儿“婊子养的”。<br />
<br />
　　其实，除了中国人，今年的世界上还有许多人有资格得这个奖，2008年的世界乱云飞渡也是英雄辈出，只要你肯选。<br />
<br />
　　但如果把眼光放在中国，就证明诺贝尔奖也想搭一搭2008年中国这趟车，这趟车被中国人自己和外国人吹嘘为“崛起之车”，大事小事好事坏事装了一车，没有人不会被中国2008年所经历的一会儿天下一会儿地下的一切所打动，一桩巨大的地震灾害和一桩完美的奥运会，就可以让世界20年之内不会忘了这一年的中国。<br />
<br />
　　所以，诺贝尔奖和平奖选中国人，也不过是种潮流作派。<br />
<br />
　　但这时，它们还要来刺刺中国。<br />
<br />
　　因为，在现在候选的中国人之外，完全有更合适的更能让今天的13亿中国人喜悦和振奋的人有足够资格领这个奖，我之前也在一篇博客里写过，中国的总理温家宝足可以凭他慈祥、急难的平民一面领这个奖，而现在诺贝尔奖委员会偏偏要找一个让中国人神经分裂的人来得这个奖，它们的目的何在？<br />
<br />
　　它们就是要让中国政府难堪，就是要让中国人神经分裂，就是要干预中国的价值观，别亦无他。我亦相信对于胡佳本人在中国的境遇，其实诺贝尔奖委员会才不会真正关心的，他们可能巴不得更多这样的人被送进监狱，这样它们就可以继续用为其授奖这样的事干预中国了。<br />
<br />
　　其实，找一找诺贝尔和平奖和诺贝尔文学奖历史上与中国有关的领奖人，如1989上获和平奖的十四世达赖喇嘛和2000年获文学奖的高行健（1988年以政治难民的身分定居巴黎市郊的巴纽里），加上现在的胡佳，他们的共同点只有一个：反共和反中国政府。*<br />
<br />
　　这样是不是可以说“反共和反中国政府”才是诺贝尔奖在和“中国”这两个字交集之后的第一立场呢？不管你有多大贡献，不管你所做的多么合乎诺贝尔和平奖要求，你必须有“反共和反中国政府”的立场，所以，在科学研究方面对中国和世界的粮食问题有巨大贡献的科学家袁隆平、在四川大地震期间感动世界的中国总理温家宝等都不可能有资格得这个奖，因为他们不“反共和反中国政府”。<br />
<br />
　　而历史上，如2007年，美国人阿尔・戈尔及政府间气候变化专门委员会国“唤醒人们对地球暖化问题的重视”而获奖，2006年，印度银行家穆罕默德・尤纳斯及孟加拉乡村银行因“从社会底层推动经济和社会发展的努力”而获奖；还有2000年的韩国总统金大中、2001年的联合国秘书长安南、1993年南非黑人领袖纳尔逊・曼德拉/南非总统戴克拉克、1994年巴勒斯坦领导人阿拉法/以色列外长希蒙・佩雷斯/以色列总理拉宾、1990年前苏联总统米哈伊尔・谢尔盖耶维奇・戈尔巴乔夫都是科学家和国家领导人可以获奖的前例。<br />
<br />
　　我说诺贝尔和平奖是诺贝尔和婊子养的，因为它们现在把“反共和反中国政府”作为得奖的与中国相关的人的第一标准。这让人对该奖项强烈的政治目的表示怀疑，至少今年的诺贝尔和平奖的目的根本不在于“和平”。<br />
<br />
　　现在这个世界DD尤其是发生了华尔街金融危机和世界经济危机的世界和俄罗斯与西方之间强烈军事对抗中的世界，更需要一个稳定、强大的中国，或者是我们自己说的那个崛起的中国DD但至少在诺贝尔和平奖候选人透露出来的信息里完全不是这层意思，而是代表西方人那种狗娘养的阴暗心态。<br />
<br />
　　就像有人家在大办宴会的时候，所有的宾客都渴望主人奉献美味佳肴的时候，你偏要夹塞端上一盘菜，里面盛的是大便。<br />
