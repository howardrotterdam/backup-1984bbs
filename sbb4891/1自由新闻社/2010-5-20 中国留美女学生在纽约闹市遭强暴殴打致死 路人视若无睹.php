<br />
<img src="http://res.news.ifeng.com/attachments/2010/05/20/rd_rs_8300447c90594c6dd9d408546f7227aa.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
将姚宇拖入后巷强奸，并被一根金属管狠狠敲打头部至脑死亡的墨西哥裔男子。（美国《世界日报》）<br />
<br />
<img src="http://res.news.ifeng.com/attachments/2010/05/20/rd_or_263890601030e0c5b19fa108e0ffa773.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
28岁西语裔男子克鲁斯涉嫌强暴、持铁棍重伤华女被捕。 （美国《世界日报》／许振辉、傅家槿摄影）<br />
<br />
中新网5月20日电 综合报道，在纽约皇后区法拉盛遭强暴攻击的23岁姚姓留美女生18日早晨8时50分经由纽约医院皇后医疗中心检查，正式被判定脑死，仅以呼吸器维生，院方还在等姚宇的父母从中国赶去看望女儿最后一眼。姚女遭强暴重殴至脑死案震惊华人社区，民代和侨界纷纷谴责凶嫌恶行，并对案发当时多位路人目击却“视若无睹”感到痛心。<br />
<br />
无端受攻击并遭性侵 受重击左头骨全碎裂<br />
<br />
5月16日晚，赴美仅两个月的23岁中国女留学生姚宇在法拉盛繁忙街区被一名墨西哥裔男子拖入后巷强奸，并被一根金属管狠狠敲打头部至脑死亡。据美国《侨报》报道，案发地点在法拉盛颇为繁忙的41路的一民宅前的停车位内。据负责此案的纽约市109分局警探Jae Shim介绍，犯罪嫌疑为28岁的墨西哥人Carlos Salazar Cruz，目前还不清楚他是否有前科。作案时，他使用一金属管打击姚宇的头部，再把她拖进一住宅前的停车位内实施了性侵害。案发后不久，警方在离现场不远的41路上抓住了他。据悉，有人在第一时间报案，帮助警方抓住了嫌犯。<br />
<br />
纽约市立医院皇后区分院外科重症治疗室(SICU)主任尼托(Neito)5月18日上午正式宣布，本月16日法拉盛41路恶性强奸攻击案受害人、中国黑龙江牡丹江籍女留学生姚宇因脑部组织肿胀及脑内压力过大而死亡。尼托主任说，姚宇遭遇的伤害是他从医多年来所见到的最严重的一起伤害案。姚宇脑部由于受到重击，左部头骨全部碎裂，其脑内的压力是正常人的3倍。<br />
<br />
尼托称，16日晚当姚宇送到医院急诊室时，他就确认姚宇没有生还的希望，做手术是出于医生的人道主义，也为了帮助姚宇增强内心的求生意志。姚宇还躺在皇后医院外科重症治疗室，整个头部缠绕着白色沙布，脑部被深红的血液渗透。她的双眼淤青，嘴唇肿大变形，全身插着各种医疗仪器的管子，对外界没有任何反应。<br />
<br />
闹市受残害路人视而不见 姚宇案折射冷漠心态<br />
<br />
姚宇16日惨遭疑犯性侵攻击导致死亡后社区人心惶惶，大街小巷民众在同情姚宇遭遇的同时，纷纷质疑案发当时为何无现场民众出手相助，眼睁睁看着姚宇被犯罪嫌疑人攻击，其冷漠心态，令人心寒。《世界日报》指出，警方在调阅街头监视录像机画面后发现，疑似喝了酒的嫌犯犯案后仓皇逃逸，但见到警车后却意识清醒地立即躲至巷内，还企图将凶器丢弃在荒草中，因此分析他是有意识的行凶。<br />
<br />
而警方也发现歹徒当街将姚女拖进巷内时，有两三位民众就在一旁伫足围观，但随即转身离去；不仅如此，目击者见到当姚女尖叫呼救时，曾有多位民众路过，却无人停下来解救或报警，眼睁睁让悲剧发生。<br />
<br />
当地华人社团18日表示，希望小区记取教训，华人不该沉默，治安也不应只靠警察维护，大家应发挥守望相助精神，一起让法拉盛成为安全、繁荣的小区。<br />
<br />
纽约华人社团联席会主席朱立创表示，在人来人往的闹市区发生这种悲剧，让他大感“不可思议”，也对民众的冷漠大叹“人心不古”。<br />
<br />
《侨报》称，美国纽约黑龙江同乡会会长李铁城表示，在同乡会和警方接洽后，在警方的带领下一起到案发现场附近调阅了监控录像，从录像中可以清晰看到案发时疑犯在攻击受害人时，路边匆匆而过的华人竟然无一人伸出援助之手，其冷漠的心态让人心寒。<br />
<br />
中美商会名誉会长付林敏芝表示，花季少女在人群熙攘的法拉盛遇到暴徒袭击后，竟然没有任何民众伸出援助之手，令人寒心。纽约华人社团联席会法律顾问陈梅表示，华人需要多多加强自身安全意识，遇到街边醉汉、疯子等情绪不正常的人，一定要多多留意，防止类似案件再次发生。<br />
<br />
针对华人特有的“多一事不如少一事”心理状态以及华人担心报警泄露身份的问题。纽约市109分局局长麦奎尔也指出，华裔社区的民众应该从自我的良知出发，不要因为担心身份泄露而纵容犯罪分子的嚣张气焰。麦奎尔再次重申，任何报警或是维护自身权益的民众，警方都不会去追查或询问其身份是否合法。<br />
<br />
华埠命案频发 华社发起华人自组巡逻队维安<br />
<br />
这宗骇人听闻的悲剧，法拉盛治安拉起警钟。这是2009年迄今华人聚居区法拉盛所在的皇后区，发生的第四起华人命案，当地居民人人自危。当地华人社团不得不发起华人自组华裔巡逻队维护社区安全。华人社团负责人19日表示，巡逻队已基本准备就绪，将在最短时间内上街巡逻，希望能避免此类悲剧再度重演。<br />
<br />
美国《星岛日报》指出，纽约华人社团联席会18日对姚姓女子的不幸难过，并采取积极行动亡羊补牢，宣布成立华裔巡逻队在晚间于法拉盛大学点大道等华人聚居的偏僻街道巡逻，保护晚上下班，特别是单身女华人安全，并加强居民提高防范罪案意识。<br />
<br />
纽约华人社团联席会执行主席朱立创、陈泰行、傅林敏芝、陈梅律师，以及美国黑龙江同乡会会长李铁城等呼吁华人发挥守望相助，有事互相照应的精神，姚姓女子惨剧才不会重演，与会的侨界呼吁华人报名加入巡逻队。<br />
<br />
朱立创表示，华裔巡逻队计划，至少三人一组，最大目的是让法拉盛居民注意防范，达到改善治安的目的。朱立创又表示，巡逻队将从晚上9时至11时这段时间在法拉盛中心区偏僻街道徒步巡逻。朱立创提醒居民，若看到可疑摸样的男子在小区附近转来转去，若还不到通知警察的地步，可及时通知巡逻队小区居民注意加强防范，通过以上举措，有助将法拉盛小区建设成稳定和治安良好的小区。<br />
<br />
