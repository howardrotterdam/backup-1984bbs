以我本身的特异功能的发挥来举例，八十年代中，中国发射了一枚人造卫星，因运载卫星的火箭燃料系统的故障而导致失败。当时我正在北京工作，就接到这个特殊的任务，国防科工委请我到中国西北部的大山区的火箭发射基地，勘察运载火箭燃料系统内的燃料均衡度。接受任务后，在航天部第四研究院沈院长的陪同下，来到了陕西省兰田的一个保密军事基地。 <br />
