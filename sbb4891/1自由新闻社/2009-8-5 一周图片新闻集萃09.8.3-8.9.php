7月21日，玻利维亚南部刮起沙尘暴。多数沙尘的厚度足以完全挡住卫星，让图像下部的地表不见。许多沙尘暴起自玻利维亚境内阿尔蒂普拉诺高原最高处的两片盐滩或高架平原。来自盐滩的沙尘暴刮往东南，再向东；规模较小的沙尘暴起自里奥格兰德（Rio Grande）和里约帕拉佩蒂（Rio Parapet）的河床，差不多是直刮向正南方。 <br />
<br />
<br />
<img src="http://static.guim.co.uk/sys-images/Guardian/Pix/pictures/2009/7/29/1248878506429/Millennium-Island-Kiribat-009.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /> <br />
无人居住的千年岛（Millennium）在2000年前又称加罗林岛（Caroline），是基里巴斯共和国的组成部分，该国共有32座环礁和1个珊瑚岛。千年岛由众多较小的小岛组成，这些小岛则由现已没入水中的火山顶周边生长起来的珊瑚礁构成。全岛最高处高出水平面6米，被认为有遭海水淹没的危险。 <br />
<br />
<br />
<img src="http://static.guim.co.uk/sys-images/Guardian/Pix/pictures/2009/7/29/1248878511795/The-Grand-Canyon-one-of-t-014.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /> <br />
这幅是美国大峡谷的卫星图，大峡谷（向图中央伸展，呈粉色、蓝紫色和灰色，与岩层类似）是世界七大自然景观之一，位于美国亚利桑那州。图中还能看到科罗拉多高原（右上角）、莫戈永高原（Mogollon，下面的黑暗区域）、米德湖（Meade，大峡谷左边的Y形湖）、内华达州拉斯维加斯市（米德湖左边的亮白及蓝色区域）和犹他州南端（左上）。 <br />
<br />
<br />
<img src="http://static.guim.co.uk/sys-images/Guardian/Pix/pictures/2009/7/29/1248878406541/The-dramatic-retreat-of-t-001.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /> <br />
左右两幅图像显示，咸海湖岸在2006年至2009年急剧回退；图中，东段80%的收缩仅仅用了3年时间。咸海湖曾是世界第二大内陆湖，但现在却成了生态灾难的代名词。50年前，苏联实施棉花灌溉计划，注入咸海的河流改道，自此，咸海湖开始收缩。 <br />
<br />
<br />
<img src="http://static.guim.co.uk/sys-images/Guardian/Pix/pictures/2009/7/29/1248878408742/Tikehau-Atoll-French-Poly-003.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /> <br />
图为蒂凯豪环礁（Tikehau）的南部，该环礁是组成法属波利尼西亚（Polynesia）土阿莫土群岛（Tuamotu）的78个珊瑚环礁之一。数块小珊瑚就像星状的斑点，点缀着广阔的青绿色环礁湖，并由植被覆盖的小岛成线环绕。在环礁的最南端，一个较大的小岛上有一个村庄，并建有飞机跑道。 <br />
<br />
<br />
<img src="http://static.guim.co.uk/sys-images/Guardian/Pix/pictures/2009/7/29/1248878502257/White-Sands-National-Monu-005.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /> <br />
横亘于美国西南和墨西哥北部之间的奇瓦瓦沙漠（Chihuahua），其北端由一片亮闪闪的白沙形成了超然脱俗的沙丘群。这些沙子由石膏构成，石膏是一种低硬度的矿物，常在水体挥发干涸后出现。虽然石膏相当常见，但石膏沙却很罕见，因为石膏易溶于水，由此可汇入外海。但是在白沙国家公园（White Sands national monument），流入盆地的河流没有出海口，最终只能挥发。多年来，石膏晶体不断结晶、溶化、风干，最终碎成微粒。 <br />
<br />
<br />
<img src="http://static.guim.co.uk/sys-images/Guardian/Pix/pictures/2009/7/29/1248878507451/the-Tambora-Volcano--Indo-010.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /> <br />
1815年5月10日，印尼的坦博拉火山（Tambora）经历了有记录以来最大强度的爆发，估计有150平方公里的火山碎屑（喷发出的火山碎屑岩和火山灰）喷涌而出；随之而出的气体在至少1300公里外的西南方也能看到，这些进入大气层的气体足以遮天蔽日，导致全球气温下降，因此，在1816年没有夏天。图中巨大的火山口直径达6米、深达1100米，它是在4000米高的坦博拉山峰崩塌、其下的岩浆房变空之后形成的。 <br />
<br />
<br />
<img src="http://static.guim.co.uk/sys-images/Guardian/Pix/pictures/2009/7/29/1248878510878/the-coastline-of-Canadas--013.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /> <br />
隆冬季节，冰层环绕加拿大巴芬岛（Baffin Island）的海岸；但夏日的阳光让其迅速消融。7月11日，应季节性的海上冰层消融正在进行。然而，自从到了21世纪，北冰洋的冰层面积急剧下滑，夏天持续缩减，冬天却恢复不多，并于前年12月创下新低。根据美国国家冰雪数据中心的数据，2009年7月上半月，海上冰层消融得比去年同期要快，但比前年慢。</div>

							
							
							
							
