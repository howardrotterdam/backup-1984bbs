													<h2>分析称谷歌在华有望赶上百度 对手优势正丧失</h2>
						
						
																											<div id="postmessage_207266" class="t_msgfont">分析称谷歌在华有望赶上百度 对手优势正丧失<br />
2009年08月18日03:08 　编译/萧谔<br />
<br />
<img src="http://img1.gtimg.com/tech/pics/20267/20267119.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
百度谷歌中国市场份额变化图<br />
<br />
北京时间8月17日消息 据国外媒体报道，businessinsider网站的博客作者麦彻同（Tom Melcher）发表文章称，虽然谷歌目前在中国落后于百度，但并不能说明谷歌无法战胜百度，相反，如果百度不在五个方面加强努力，谷歌将赶上并超越百度。以下是文章的主要内容：<br />
<br />
谷歌在美国搜索市场占据了64%的份额，在很多应用领域都处于领先，似乎谷歌是不可战胜的。但中国是个例外。<br />
<br />
在那儿，谷歌远落后于中国领先的搜索引擎百度，后者占据了中国搜索市场60%份额，而谷歌只有30%。最近百度发布了创纪录的季度业绩，股价也接近1年里的最高点（市值为120亿美元，5倍于IAC）。<br />
<br />
到底发生什么事了？为什么百度在中国这么强？而谷歌这么疲软？如同很多其他美国公司一样，谷歌注定无法在中国取得胜利？我的回答是：不，谷歌不会失败。<br />
<br />
事实上，如果是我掌管百度，我会很担心--不仅是担心谷歌。我认为百度的成功是基于5个关键因素，而这些因素目前都在恶化。<br />
<br />
首先，百度的搜索结果历史上一直比谷歌好，但现在这种情况正在发生变化。<br />
<br />
与其他任何国家一样，本地化非常重要。数年前，我在谷歌中国和百度上分别输入SHE，谷歌搜索引擎认为我是在找代词“她”，而百度正确理解到我是在找台湾著名摇滚乐队S.H.E。<br />
<br />
不过，随后谷歌的搜索结果开始变好，在你看到CNNIC提供的中国搜索市场份额变化图时，很容易发现这种趋势。<br />
<br />
在过去的5个季度里，谷歌市场份额上升很快。通过我与中国网民的谈话，我的感觉是谷歌最终将注意力转到中国，开始注重搜索质量。同时，中国网民也在快速分化。谷歌多数结果是根据算法相关性排定，广告只出现在页面右边栏。<br />
<br />
与之对照，百度过去一直是向广告商销售所有搜索结果（包括主要结果和页面右边栏）。因此，在百度的前几页上充满了付费最高的公司广告，而谷歌的页面则是显示最佳结果。<br />
<br />
中国喜欢谷歌的最好证据是，百度最近推出的“鸟巢”设计。百度在页面左边只销售最好的4个结果，这表明该公司最终放弃了原先的做法。在这些广告之后，按照算法相关性排定结果。我猜，百度之所以做如此大的改变，是注意到了很多用户喜欢谷歌的做法。<br />
<br />
现在百度和谷歌看起来如此接近，我的感觉是谷歌的市场份额将继续上升。因为谷歌规模更大、利润更高，能雇佣更多的技术人才。虽然两家公司在北京都有很大的工程中心，不过我的朋友认为，在谷歌工作比在百度更酷。在搜索竞争上，人才真的很重要。<br />
<br />
第二，百度发明了一些非常流行的、谷歌没有的应用，但谷歌将迎头赶上。百度第一个杀手级应用是音乐搜索，很容易找到和下载盗版音乐。另一个是百度收购的Hao123，一个类似初期时候雅虎的网站链接目录。最后是百度的贴吧，一个符合论坛应用要求的搜索引擎。所有这些应用都只能说明百度现在的成功。<br />
<br />
然而，过去2年里，谷歌中国团队开始反击并取得成功。最近谷歌推出了自己的音乐服务，允许用户免费下载未加密的音乐文件。想像下一个免费的、与谷歌一样快搜索的、不分硬件的iTunes音乐店会是什么。另外谷歌也推出了与Hao123类似的网站链接服务。<br />
<br />
第三，百度的优势过去是靠规模很大、但过时的销售团队。而谷歌最终将解决这个问题。当你签约百度成为广告商后，你会有一个自己的销售代表，他很乐意为你解释如何发布广告、如何接收订单。而谷歌自动的AdWords系统却令人望而生畏。直到最近，在中国购买谷歌广告都需要一个国际信用卡，因为谷歌中国还未发行官方的中文收据。这个销售网络不仅能为百度带来收入，还扩大了百度的影响。<br />
<br />
但现在情况开始改变。谷歌开始接受多种形式的支付，可以开中文收据甚至有免费热线电话。同时，百度的客户也强烈要求谷歌提供的简单、透明的广告购买程序。这些广告商也质疑百度提供的广告业绩报告，要求获得可信的和易于使用的分析工具。<br />
<br />
总之，由于中国广告商日益精明，百度的传统销售力量不再像过去一样有用，而可能是一种高成本的负担。我开始收到被百度解雇的销售人员申请，也听到百度销售人员因佣金减少而辞职的事情。<br />
<br />
第四，百度以前得到了中国政府的大力支持，但现在也在改变。有关这种支持的性质和范围过去存在很多传闻，不过多数观察人士都认为，中国政府急于扶持本土企业，偏好百度而为谷歌设置障碍。不过最近也出现了变化。百度的商业行为被CCTV点名批评，我的朋友认为，百度的骄傲自满令政府无法忍受。<br />
<br />
虽然CCTV随后又批评了谷歌中国，但没有什么新的东西。事实上百度可能无法再从政府那里获得无限制的支持。<br />
<br />
最后，百度正在失去焦点。与谷歌一样，百度曾有着清晰而强烈的使命感，只关注于国内市场。不过最近百度开始将大量注意放在国际扩张上，特别是在日本。同时，百度正在招揽媒体协议（最近与探索频道签约）。<br />
<br />
综上所述，百度的5大传统优势正在失去，很大部分是因谷歌在中国采取更为积极的行动。同时，因竞争加剧，百度自己的同盟本地公司也都不耐烦。最有趣的战场是电子商务。很多人都认为，传统上阿里巴巴是百度最大的广告商，但去年百度宣布推出自己的B2C平台，与淘宝竞争。<br />
<br />
据说阿里巴巴随后停止了购买百度的广告，但未对阿里巴巴的流量产生真正的影响。同时，进入电子商务领域，百度也与以前的同盟者腾讯开战。<br />
<br />
不过，百度的收入和利润可能会继续增长，因为中国的搜索市场发展如此之快，而广告商在搜索广告上的选择很少。我也认为百度的股价将继续表现优异，因为中国网络是一个很好的投资版块。<br />
<br />
但百度作为中国的谷歌杀手的日子已经屈指可数了。</div>

							
							
							
							
