　　俗话说，擒人先擒王，射人先射精。白白决定找村长下手。她某天深夜找村长床上长谈，当然少不了先免费招待一下。村长抽事后烟的时候，白白说话了：村长呦，你不知道，那个姑姑经常在背后损你，老跟别的村民说你的坏话，你睡她的时候，什么都被她看光了，所以啊，其他村民一上她，就知道你有几枚痔疮，几块烂疤，还有狐臭早泄，你的形象全被毁了啊。 <br />
　　<br />
　　村长大怒，拍床而起。随即叫村丁过来，把姑姑的大门锁了，还上了封条。从此，村民们都上不了姑姑了，只好来上白白。那姑姑虽是尤物一个，但人在屋檐下，不得不低头，斡旋了好久，终于勉强再次开张，但从此牙关紧闭，不敢胡乱说话，村民们都叫她哑姑。经此一折腾，姑姑不复当年之勇，村民们上白白已经成为了一种生活习惯，很少再敲姑姑的门。那白白日渐滋润，更是丰腴，卓然已成本村红灯业之旗舰。 <br />
　　<br />
　　子在川上日（抱歉，打错，应为曰）：有需要，白白一下。白白姐姐觉得这话不错，遂裱在阁楼上做了大幅广告。万千嫖客，无不景仰。有一日，那白白寻老流氓帮想几句新广告词，老流氓沉吟片刻，挥毫写下：天下精虫眷恋的地方。</div>

							
							
							
							
