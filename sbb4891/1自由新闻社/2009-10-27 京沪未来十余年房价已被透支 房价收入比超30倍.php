																											<div id="postmessage_301969" class="t_msgfont">京沪未来十余年房价已被透支 房价收入比超30倍<br />
2009年10月27日来源：经济参考报 　<br />
　学者羊慧明等业内人士表示，如果国内一线城市房价继续被哄抬炒高，将可能重蹈东京楼市“急涨长跌”的覆辙。一些经济界人士认为，当前我国一线城市的房价已经很高，风险已经很大，前车之鉴不能不重视。<br />
<br />
　　事实上，北京、上海的人均收入还不到东京的1/10，而房价已比肩东京，一套100平方米的普通公寓(清水房)房价达200万元人民币以上，即以家庭户均年收入6万元计算，房价收入比已超过30倍。 <br />
 <br />
<br />
<br />
　　京沪人均收入要达到东京的水平，起码还要15年。换言之，京沪房价已经把未来十余年的空间都透支了。日本的地产泡沫是一个富裕社会的泡沫，都拖垮了日本经济，而中国还是一个整体不富裕的社会，出现超过日本的房地产泡沫，其灾难性后果将比日本更可怕。<br />
<br />
　　专家们认为，我国并不是没有发生过房地产危机。海南、香港都曾因房地产泡沫破裂受严重拖累，深圳去年的房价大跌引发部分楼盘业主集体“断供”，风险陡然出现；杭州房价下跌幅度曾达到几千元，市场低迷持续时间超过其他城市。<br />
<br />
　　从一个国家来看，房地产沉淀的社会资金越多，对其他领域的发展越不利，没有一个国家因房地产发达而成为世界强国。从一个企业来看，沉迷在房地产就会丧失斗志，荒废主业。<br />
<br />
　　日本泡沫经济时代购买不动产越多的大企业，受到的拖累就越大。当前，内地楼市正在克隆香港的模式：一旦把“地王”炒高到世界之最的天价，拿得起地的开发商就越少，最后形成寡头垄断，他们就可以随意拔高房价，牟取惊人的暴利。<br />
<br />
　　靠非理性疯狂拉高房地产等资产价格拉动的经济增长是不可持续的，并可能使整体经济和社会受害，最终榨干社会财富。内地不可能仿效香港的房市，仿效香港地产模式将祸害中国社会。 (来源：经济参考报)</div>

							
							
							
							
