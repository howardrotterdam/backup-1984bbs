丹麦哥本哈根的联合国气候会议，本是发达国家和发展中国家的利益冲突，前天（12月18日，周五）更因美国总统奥巴马（ Barack Obama）公开抨击北京拒绝接受减排审核，变成中美领袖意气之争。中国总理温家宝觉得受辱，拂袖离场，只派低层官员跟奥巴马开会，使会议毫无进展，奥巴马为免峰会无法达成协议，不惜踩场求见：「总理先生，你准备好见我吗？」最后在中美和三个新兴国家密斟下，达成了一份「小圈子协议」，要 193个国家接受的《哥本哈根协议》。<br />
<br />
－－－－－－－－－－－－－－－－－－－－－－－－－－－－－－－－－－－<br />
<br />
经过两年会谈，加上过去两周气候会议密集商讨，中美已多次就减排温室气体目标、富国援助穷国减排、审核发展中国家减排问题上唇枪舌剑；当温家宝跟奥巴马碰头，更擦出一场火药味极浓的「中美大战」。<br />
<br />
「希望跟可作决定的人谈判」<br />
 <br />
12月18日早上，中国不满美国再就审核发展中国家减排大做文章，率先「出招」。约 20国领袖和部长举行紧急会议，刚抵兜陌掳吐硪灿谐鱿，中国只由副外长何亚非作代表。<br />
温家宝其后在大会上现身发言，指不管峰会得出甚麽结果，中国会全面履行自行制订的减排目标。奥巴马随后登场发言，猛批中国拒接受减排审核：「我不知道，当大家不互相提供资料，保证履行了责任，怎麽能有国际性协议可言。这并不合理，那只会是空洞的胜利。」<br />
温家宝觉得奥巴马这番话是对中国诚信的羞辱，板着脸拍掌，然后即时拂袖离场，返回酒店，指派低层代表继续出席会议。虽然他与奥巴马中午举行了双边会谈，但在下午4时的多边会议，他再度缺席，只派了低官阶代表于庆泰等三人做代表，奥巴马很不满，毫不客气地说：「我不想再纠缠了，我希望跟可作政治决定的人谈判，只想跟温总理会谈。」<br />
奥巴马知道与会者立场南辕北辙，呼吁大家宁要一份不完美的空泛协议，也不要空手而回，否则后果堪虞。他傍晚 6时，决定亲到酒店和温家宝作第二次双边会谈，但谈不出结果。他遂希望跟其他新兴国家领袖会面，吩咐助手联络印度、巴西和南非代表。但美方得到的回覆，印度总理辛格（ Manmohan Singh）已到了机场准备离开，而巴西和南非亦无意跟他会谈。 <br />
<br />
不够座位　奥巴马：无问题<br />
 <br />
没多久，中方致电美方，指温家宝已准备好再跟奥巴马会面，美方派员先到会议室，不得其门而入，却发现了温家宝、辛格、巴西总统卢拉（ Luiz Lula da Silva）和南非总统祖马（ Jacob Zuma）正开秘密闭门会谈。<br />
<br />
奥巴马得悉后决定「踩场」，有官员担心会出现「四对一」不利局面，但他表示「无问题」，因为不想中印等四国秘密谈判。奥巴马立即率领国务卿希拉莉（ Hillary Clinton）「踩场」，在会议室门外当着大批记者和领袖面前大声喊门：「总理先生，你准备好见我吗？你准备好了？总理先生，你准备好见我吗？你准备好了？」<br />
<br />
会议室内的官员指不够座位，但奥巴马为求入内，遂说：「没问题，我坐在老友卢拉旁边。」说罢冲入会议室，把一张椅推到卢拉旁坐下，跟四国领袖谈判。五人谈判了 45分钟后，终达成协议草桉文本，取代了之前 119国领袖审议的八个协议草拟文本，供大会表决。<br />
<br />
中国在会议上「企硬」，迫使美国屈服，成功维护了国家主权和利益，凸显中国国际地位进一步提升，中美「两国集团」（ G2）正在成形，主宰全世界。但分析指中国作为发展中国家的代表，为了自身利益，却牺牲了其他发展中国家利益，影响了中国的国际形象，也可能进一步激起西方国家的反华情绪。<br />
<br />
英国《独立报》/《卫报》/美国《纽约邮报》</div>

							
							
							
							
