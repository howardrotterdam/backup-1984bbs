																											<div id="postmessage_709318" class="t_msgfont">最新研究报告称91%中国民众认为受到尊重对待<br />
<br />
2010年06月28日 20:18　来源：新华网　<br />
<br />
<br />
　全球知名研究咨询公司盖洛普最新公布的2009年中国民生指数及相关研究结果显示，2009年中国民众认为受到尊重对待的比例为91%，比2008年提升了2个百分点。<br />
<br />
　　盖洛普公司从2005年开始进行民生研究项目调查，至今已在130多个国家和地区展开，全球人口覆盖率达到95%。所编制的民生指数包括总体生活满意度、总体生活改善满意度、生活状况满意度、公共政策满意度等四个方面。其中，感觉是否受到尊重是最重要的调查指标之一，也从最直观的角度反映了民生总体状况。盖洛普发现，在中国，性别、年龄以及城乡差异并未导致公众的受尊重感有显著差别，比例大致相同，但较高的家庭收入和教育水平会令公众的受尊重感明显较高。<br />
<br />
　　盖洛普认为，在生活状况满意度对受尊重感的影响方面，家庭生活对中国民众的影响最大。对家庭生活满意的中国民众认为受到尊重的比例达到94%，而对家庭生活不满意的中国民众，认为受尊重的比例只有81%。<br />
<br />
　　相比之下，对工作满意及不满意的中国民众，认为受尊重的比例分别为93%和87%；对家庭收入满意及不满意的中国民众，认为受尊重的比例分别为93%和88%；对居住地满意及不满意的中国民众，认为受尊重的比例分别为92%和87%；对个人健康状况满意及不满意的中国民众，认为受尊重的比例分别为92%和87%。 <br />
<br />
<br />
　　盖洛普表示，自2006年开始在中国进行民生研究项目调查以来，中国民众认为受到尊重对待的比例基本保持稳定。<br />
<br />
　　盖洛普咨询公司总裁吉姆・克立夫顿表示：“民生指数为我们提供了另一个了解中国社会变化的角度，有助于全面展示中国社会的变化发展状况。”<br />
<br />
　　数据显示，世界各地的民众受尊重感比例与当地的发达程度并无直接关系，发达国家和地区民众受尊重比例并不一定高于发展中国家和地区。比如，美国2009年民众受尊重比例为89%，而相邻的加拿大和墨西哥都达到93%。日本及韩国的民众受尊重比例为62%和69%，菲律宾和印度尼西亚都达到94%。<br />
<br />
　　对此，盖洛普中国首席咨询顾问吴涛博士表示，经济和社会发展的现状并不是影响民众受尊重感的唯一因素，更重要的因素是与过去相比生活状况是否得到了改善，以及判断在未来能否得到进一步改善。吴涛同时表示，受到尊重对于个人来说是重要的需求之一，也是显示民生状况的一项重要指标。较高的民众受尊重比例，对于社会的稳定、和谐及健康发展必不可少。（记者周文林）<br />
