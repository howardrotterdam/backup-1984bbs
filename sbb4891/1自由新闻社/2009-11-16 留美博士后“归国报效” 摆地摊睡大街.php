													<h2>地摊博士后11月18日到北师大面试(图)</h2>
						
						
																											<div id="postmessage_333083" class="t_msgfont">　　11月18日本报报道了海归博士后孙爱武在北京摆地摊露宿街头数月一事（见本报昨日A12版）。由于目前天气寒冷，近期孙爱武一直没有回到他摆摊的市场。在得知孙爱武的事情后，中科院院士、北师大教授李小文18日约见了他，并提出了聘请其为专家的想法。但是由于双方在待遇方面的分歧较大，目前双方暂时尚未就此事达成共识。<br />
<br />
　　18日上午，记者再次来到孙爱武“蜗居”的摊位，他始终没有出现。周围的摊主告诉记者，这几天天气转冷，他很少过来了。但是他的货品、电脑、被褥还都留在原处，居民们都在帮他照看着，一件都没丢。在他的大舅哥刘全胜的帮助下，记者终于见到了孙爱武。黑框眼镜、络腮胡子、深色棉服、牛仔裤，尽管经历了几个月的流离失所，孙爱武看起来有几分落魄，但言谈举止仍透着浓厚书卷气，未失知识分子的“体面”。<br />
<br />
　　在媒体报道了孙爱武的事情后，中科院院士、北京师范大学地理学与遥感科学学院教授李小文通过刘全胜向孙爱武发出了面试邀请，在孙爱武在得知后也同意“赴约”。在面试过程中，孙爱武并没有带自己的简历，不过李院士已经从网上为其下载了一份，并对其学术研究能力表示肯定。面试在轻松的聊天气氛中进行，甚至看起来都不像是一场面试。由于对孙爱武的情况已经比较了解，李院士只是和他谈起了在纽约当博士后的一些相似经历，并没有对各项科研成果进行深究，两人很快进入到了待遇问题探讨。<br />
<br />
　　李院士表示，北师大基于孙爱武已有的学术成绩表示乐于聘请其为专家，帮助其租房、安家，并开出月薪4800元的专家费。据称，这在目前的专家待遇中可谓优等。<br />
<br />
　　但对此，孙爱武仍表示难以接受，他希望可以直接入校后“破格”成为博导，自己带学生，做科研，并有一定的资金支持，而这样的要求与目前不少高校的现实程序存在很大差异。双方18日暂时尚未达成共识。<br />
<br />
　　对话<br />
<br />
　　“从来没遭过这份罪”<br />
<br />
　　和孙爱武的谈话是支离破碎的，因为在和他接触的时间里，孙爱武一直在忙着其他的事情。下面这段孙爱武的对话同样是支离破碎的，抛开那些不着边际的话语，记者的这段对话试图能保留他生活中最为现实的一面。<br />
<br />
　　记者：您在美国从事什么工作？为什么选择归国？<br />
<br />
　　孙爱武：就是做一些基础性的化学研究。我也没有选择回国啊，就是回国来看看，我走的时候还是请了一个月假走的。后来回来这个、那个事情就耽误到了现在。<br />
<br />
　　记者：那您打算在哪儿发展？还计划回美国吗？<br />
<br />
　　孙爱武：这个现在不好说。美国也不是说回去就能回去，还要有签证等各方面的情况。<br />
<br />
　　记者：如果在国内发展，什么样的工作能令您满意呢？<br />
<br />
　　孙爱武：我搞化学都20多年了。我能干什么，就是当博导、研究员。能给我建立一定水平的实验平台，能让我带课题，搞研究呗。<br />
<br />
　　记者：那待遇方面呢？<br />
<br />
　　孙爱武：我这个人一直没有什么奢求的。但是我有老婆孩子，我有三个孩子，我们全家从来没有分开过，我们要在一起啊。首先要给我一套大房子，有一定的安家费吧，这是一大笔。至于每个月，我就要求很少了，两三万，三五万人民币够花就行了，如果就给我万八千元，也不够生活啊。<br />
<br />
　　记者：1万元应该够吧？<br />
<br />
　　孙爱武：别人可能都够。但是我不行啊，我总是丢东西，丢钱。每天丢个一二百元很正常。而且还有人找给我假币，我有各种版本的假币，我都留作收藏了。<br />
<br />
　　记者：您目前选择的摆地摊儿的方式，能赚到钱吗？<br />
<br />
　　孙爱武：我摆摊儿也不是为了赚钱，我回国了，没有工作，人总要找个事情来做嘛，所以我才摆地摊的。<br />
<br />
　　记者：但是每天风餐露宿的，和您在美国的生活差异也很大吧？天这么冷了，您晚上住哪儿呢？<br />
<br />
　　孙爱武：我在美国生活条件还是挺好的，也有暖气啊，有房子啊。当然和这种情况没法比了，从来没有遭过这份罪啊……现在，我就住住小旅馆，但是旅馆的条件差，“有毒”，我换了多少个了，也不行。我这段时间住旅馆都花了4000多元了。<br />
<br />
　　记者：为什么不住亲戚家？或者租个房子？<br />
<br />
　　孙爱武：他（大舅哥）家房子也小，我们全家怎么住得下，再说那也不是长久之计啊。而且我（对房子要求高，有异样的气味绝对不能忍受）住旅馆，换了那么多都不成，要是租一个房子，发现不行，人家也不给退押金，更赔。<br />
<br />
　　记者：那你的家人怎么办？<br />
<br />
　　孙爱武：说的是啊，我在美国也压力大，但是我们全家一起啊。我觉得全家一起是最重要的。这也不知道怎么搞的，我和我老婆孩子都分开两个多月了。我找了工作，当然就要把他们都接过来，一家人在一起，我们从来没有分开过这么久。<br />
<br />
　　编后<br />
<br />
　　但愿他是下一个纳什<br />
<br />
　　电影《美丽心灵》中，诺贝尔经济学奖得主约翰&#12539;纳什在人生的辉煌时刻患上了精神分裂症，饱受疾病困扰的他一度成为人们眼中的怪人，眼前孙爱武的情况和纳什似乎有几分相像。<br />
<br />
　　密歇根大学的同事朋友说，孙爱武回国前曾饱受精神疾病的折磨，并且在美国相关医院求诊。“当时周围很多朋友也提供了不少直接的帮助和照顾。然而看到事情发展到这个地步，感到很痛心。希望能够借助于公布这些实情，引起他的家人和社会的关注来帮他治好病。”他们通过网络发帖等方式，希望公众和媒体对孙爱武能够“留情”，希望有人能够先帮助他完成治疗。<br />
<br />
　　有人认为，社会没有必要去帮助一个被自己淘汰的精神病患者，对于摆地摊的博士后，同情和鄙视的意义是一样的。然而帮助似乎并不仅是意味着泛滥的同情，况且需要帮助的并不只是孙爱武一个人。纳什在妻子的帮助下最终走出了疾病的困境，而孙爱武面对的却是同样“歇斯底里”甚至情况更为严重的妻子。<br />
<br />
　　抛开那些不着边际的表现，现实中的孙爱武仍不失为一个学者、专家。如果他能走出当下的困境，或许他得不了诺贝尔奖，但他至少能够收获美丽的心灵。（记者：陈凯一）<br />
