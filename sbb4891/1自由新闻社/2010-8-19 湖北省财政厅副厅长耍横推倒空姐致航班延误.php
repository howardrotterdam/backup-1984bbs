&nbsp; &nbsp;果然一会儿电话通了，但似乎因为刚好机舱里是满客，跟董事长打了电话也没有搞定。接着，他说对同意按空姐提出的办法执行的夫人及随行人员说到：“你到底是跟哪个一伙的，你搞清楚!今天不能坏了规矩，我给她上一课!” 空姐无奈地又找出了机长跟他再次数出示安全规定，并劝说到空姐到地面给你办托运，不用你走半步。此时航班已经推迟关门八分钟，而空姐为了不将事件扩大，一直广播通知旅客飞机的随机文件未到请大家稍等。<br />
&nbsp; &nbsp; 这位网友说，我本人乘坐飞机二十余年，也是南航的金卡旅客，我从未见过可以将轮椅这样的大件放在波音737这样的小飞机上的事儿。 <br />
