<br />
　　有星期一乘坐南航返回武汉的网民爆料，今天(8月16日)他乘坐南航CZ6652航班返回武汉，但因为湖北省财政厅副厅长周顺明因不同意把其夫人所用轮椅进行托运而发脾气耍横推倒空姐，导致航班延误的发生。<br />
