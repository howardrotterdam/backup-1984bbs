　　成绩好不好会分圈子、玩不玩名车会分圈子、用不用名牌会分圈子、是不是同性恋也分圈子。“所谓圈子，有大有小，有具体有抽象，有某种共同点，朋友的朋友也能串联在一起，然后一起从事一些特定的事情，例如钓鱼、打牌、旅游、逛街、夜店、飙车，好坏都有。”<br />
<br />
　　因为圈子的影响，原来很朴素的女孩，买了第一个GUCCI后，下一个目标就是LV，然后再准备是CHANEL。陈阳说，类似的例子“很多很多”。<br />
<br />
　　再夸张一点圈子就像初高中时小男生组建的所谓“帮派”，一个圈子的人看另外一个圈子的某人不爽，就会出现对立的圈子。“互相之间有了问题，可能还会找社团出马”。社团就是黑社会。“学生想要和社团接触很简单，没什么特别距离。”陈阳自己就认识几个社团人士。<br />
<br />
　　至于加入到哪个圈子，有很多偶然因素。“其实你想要认识怎么样的人都很简单，不是说那些豪车的就多遥远或者需要付出什么代价，关键是你想不想呆在那样的圈子”。陈阳说，他坚持自己的交友原则是：不嫖不赌不毒。<br />
<br />
　　“明白了吧？这就是我们的预科生活。这样的环境，可想而知对有些预科学校来说到底是赚钱重要还是把你送进大学重要。所以，教育部发预警，我一点也不奇怪。”<br />
<br />
　　预科班相当于培训学校<br />
<br />
　　对教育部的预警，陈阳一点也不觉得意外。多数国人对“出国留学”仍有盲目崇拜之心，并且简单地以己之国情推论他人，以为大学都是公办的，不存在倒闭一说。把孩子送进预科=送进大学=已经成材。<br />
<br />
　　其实呢？陈阳做了一个简单的比喻：澳洲的预科，就相当于国内的“培训学校”。<br />
<br />
　　我读的预科学校在悉尼很有名，学费也很贵。它针对很多大学搞了“培训班”，可以帮助你申请你想上的大学或专业。<br />
<br />
　　澳洲大大小小的预科学校不少。一般区分预科好坏的标准，是看它能把多少人送进他们想要读的大学和专业。当然，预科多数是私人办的，不可能保证所有经过培训的学生都能上大学。我们学校就有人读了三年还上不了大学。我的同学里，有28岁的，也有18岁的。<br />
<br />
　　搞笑的是，今年我们学校预科班申请悉尼大学的通过率特别高。几张晃悠了好几年的老脸都进了。有传闻说是预科班里人太多，塞不下。那些演讲课、论文什么的，老师就给学生打高分，赶紧送出去。不过别以为他们赚便宜了，这批人进了大学就惨了，国外大学宽进严出，估计很难毕业，只有烧钱的份。<br />
<br />
　　据说陈阳的预科学校原来只有一层楼，后来和中国的中介关系搞好了，源源不断送进来的学生坐满了整整四层。现在学校里一半以上的国际学生都是中国人。还有人干脆毕业后就留在澳洲办预科学校了。<br />
<br />
