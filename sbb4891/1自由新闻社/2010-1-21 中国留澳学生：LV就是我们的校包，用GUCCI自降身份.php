　　有一次，我们一个同学背着LV经过悉尼海关，女报关员问他的LV包多少钱，听说是1800澳元后，立即开玩笑说，“你好有钱，我想嫁给你”。<br />
　　前一段时间，澳元的汇率大跌，从6块5跌倒4块多，LV的价格没变，我陪着几个女生去血拼，每人买了三四个。有点钱的男生还会把LV当礼物到处送。当然，也不排除没钱的人啃咸菜省好几个月的钱去买包，或者，再简单一点，找个男人让他送。<br />
<br />
