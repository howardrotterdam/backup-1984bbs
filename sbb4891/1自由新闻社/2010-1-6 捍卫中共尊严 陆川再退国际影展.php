<br />
　　不满第21届棕榈泉电影节展映“藏独”主题纪录片，陆川的《南京！南京！》和电影《超级50》坚决退展<br />
<br />
　　新年伊始，中国电影人再次捍卫了自己的尊严！<br />
<br />
　　第21届棕榈泉国际电影节4日遭到了中国影片的一致不满：为了反对该电影节将放映“藏独”主题的纪录片《云后的太阳》，由陆川执导的《南京！南京！》和小成本电影《超级50》已决定退出本届电影节。<br />
<br />
　　据悉，本届电影节将于7日在加利福尼亚正式开幕。<br />
<br />
　　导演陆川：<br />
<br />
　　一切以国家利益为重<br />
<br />
　　记者4日晚连线《南京！南京！》导演陆川时，他表示，自己4日早上才知道会有“藏独”题材纪录片参展，并已第一时间决定退出影展。<br />
<br />
　　“一切以国家利益为重，我们的态度就是必须而且没有任何商量余地地退展。”陆川说，他已经向发行方和电影节都表达了坚决退展的意愿，但由于操作的过程还需要时间，他也只能继续保持关注。“理论上已经不是由我来处理了，但作为导演，我也关注影片的命运，比如我听说现在已经提交了正式的文书要退赛，对方也正在处理。”<br />
<br />
　　当被问及电影节方面是否作出回应时，陆川称，据他所知，电影节方面对此“比较遗憾”。“因为这个影片在海外影响还是挺大的，他们也非常重视这个影片，所以我们退赛给他们挺大震动。”陆川坦言，作为中国人，在维护国家的事情上，一定要态度一致。<br />
<br />
　　《超级50》：<br />
<br />
　　退展是惟一选择<br />
<br />
　　另一部参展的中国影片《超级50》则是一部讲述了普通上海老百姓通过舞蹈梦想成真的故事。<br />
<br />
　　据悉，原本除了参加电影节展映外，《超级50》还会参加专为首次制作电影的电影人设置的竞赛单元。但在获悉棕榈泉电影节将放映“藏独”主题纪录片的行为后，影片出品方面在第一时间向组委会表达了退展退赛的意愿，并于4日晚起草了一份正式退展声明：“我们获悉本次电影节将放映有关‘藏独’主题的纪录片，这一行为存在严重的政治误导，并伤害了中国人民的感情。本公司经过慎重考虑，决定退出本次电影节。”<br />
<br />
　　影片出品方发言人王亮告诉记者，影片最初根本没有申请参展，是棕榈泉电影节方面特别发来的邀请，“当时电影节的负责人说他们对《超级50》非常看好，觉得中国当代的中老年人有这么好的面貌，很惊讶。”但是现在发生了这样的事情，王亮说：“退出展映和评奖是我们惟一的选择。”<br />
<br />
　　据《新闻晚报》<br />
