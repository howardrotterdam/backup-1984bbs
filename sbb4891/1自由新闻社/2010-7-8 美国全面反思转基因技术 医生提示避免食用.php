问一问以制造反转基因话题为荣的记者金微<br />
不是钟馗也打鬼<br />
<br />
第一次见识《国际先驱导报》的名记金微，是他的一篇《转基因大米：想说爱你不容易》（2010/02/04）报道。在这篇报道中，他引用了中国人民大学农业与农村发展学院副院长郑风田，环境保护部生物多样性研究首席专家薛达元，中国科学院植物研究所首席研究员蒋高明，“绿色和平组织”食品与农业项目主任方立峰，中央民族大学教授张宏良等四位人士的反转基因言论。通过这些反转基因人士的口，金记者提出了三个论题：转基因水稻的安全吗？专利费是否是我国的粮食主权埋下的‘定时炸弹？审批者是哪些人？但遗憾的是金记者没有做进一步的调查研究，哪怕象征性地采访几位研究转基因专家或管理官员也没有，而是自问自答，结论的客观性和公正性就可想而知了。<br />
<br />
但金记者按捺不住内心的得意，在随后一篇博文“转基因主粮争论空前”中他这样写道：我们的转基因报道转到网上是在出报后的第5天，但立即成为四大门户网的热点话题，可以说我们的报道左右了这家主流期刊的舆论方向，可以说，我们有一定的制造话题的实力。两会前后，仍是暗流涌动的话题。广东卫视新年第一期的《郎眼财经》，话题正好是转基因大米，郎咸平和主持人在节目中大多都引用了我们的报道，并且两次提到我们报纸名字，这在一定程度上对我们是种宣传。一定程度上，我们制造了转基因的舆论话题，但并没有深入进行跟踪报道，显得后继乏力，为他人嫁作衣裳。”一席得意洋洋的自白，暴露出这位金记者借反转基因之名行宣传自己之实的丑恶嘴脸。<br />
<br />
进一步见识金微是他的另一篇报道“记者观察：转基因，一场新的鸦片战争”。他把《粮食危机》作者、美国人威廉恩道尔夸大其辞的一句话“我更倾向于把转基因工程的推进比作新一轮的鸦片战争”做为报道的标题，用大量篇幅介绍《粮食危机》中列举的一个阿根廷的农业是如何被孟山都控制的实例：“转基因大豆的机器播种方式，使得害虫和杂草泛滥。为了除掉杂草，农民不得不使用孟山都的抗农达除草剂。与转基因作物种植面积一同上升的是阿根廷的贫困人口。生活在贫困线下的人口比例，在1970年仅为5％，1998年变为30％，到2002年增至51％” 但事实的真相是：2007年阿农畜产品年出口值近240亿美元，是世界最大的豆粉出口国，是玉米和高梁的第二大出口国，是大豆的第三大出口国。阿2007 年大豆总产量达4748万吨，在拉美仅次于巴西，列第2位，在全世界居第4位。中国是阿根廷最大的大豆消费市场，阿根廷出口的转基因大豆四分之三销往中国。另一方面，中国是向阿根廷供应农药的主要国家，占到了41%，其次是巴西，占到了16%，美国占15%。《粮食危机》这样一本充满谎言和冷战思维的畅销书，金记者为己所用，不加分析和批判，其制造反转基因话题扩大影响的险恶用心昭然若揭。<br />
<br />
本月6日，金记者和特约撰稿“直言了”炮制出一篇“美国全面反思转基因技术挑战天然转为尊重天然”报道。该报道秉承金记者历来不客观公正的报道立场和原则，断章取义，混淆是非，完全丧失了一个记者最基本的职业道德。此外，他特邀的撰稿人“直言了”又是什么货色呢？“直言了”是一个臭名昭著的造谣大王，竟然篡改美国国家科学院报告的内容，为反“转基因”提供科学理论依据。美国科学院的报告里面根本就没有老鼠血细胞和肝细胞异常；中西部农场出现猪假孕或不育；德国母牛非正常死亡；鸡死亡率高2倍；英国过敏症上升50%；菲律宾出现小肠和呼吸系统异常反应和细菌基因/蛋白可能遗传给下一代等等的内容。“直言了”却将这些内容编造进《1997年以来全球转基因食品健康损害事件一览》和《转基因食品的研究现状》等文章中。“直言了”还编造了一个美国BT转基因作物种植面积下降的数据：美国BT转基因玉米种植面积从2005年度的27%降低到2009年的17%，减少了10%左右；同期、BT棉花则从18%降低到 17%。而事实上，美国农业部网站数据表明无论是BT转基因玉米，还是BT转基因棉花的种植面积比例，从2005年到2009年都是上升的。BT玉米从大约36%上升到59%；BT棉花从大约45%上升到62%。二人的德行可谓珠联璧合。<br />
<br />
在一篇题为“不要以种植转基因水稻为荣“的博文中，金记者颇有感触的写道：“媒体秉承的是客观公正的报道立场和原则，既然你采访了这么多支持的意见，也需要听一回反对者的说法，这是一个记者最基本的职业道德。”我想问一问金记者，做人就要言行一致你知道不？客观公正的报道立场和原则你遵守了多少？记者最基本的职业道德你还有吗？</div>

							
							
							
							
