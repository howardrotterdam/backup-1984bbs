转基因技术在发展过程中都伴随着激烈的安全争论。金记者在这篇报道中列举了转基因产业化发展过程中的几个安全性争论事例。该报道介绍，2005年俄罗斯公布了转基因大豆喂养小白鼠的试验报告。俄罗斯女生物学家叶尔马科娃在研究中发现转基因食品影响小白鼠及后代的健康：在小白鼠交配前两周以及在它怀孕期间，喂食经过遗传基因改良的大豆，一半以上的小白鼠刚出生后就很快死亡，幸存的40％生长发育也非常迟缓，它们的身体都比那些没有吃这些大豆的小白鼠所生下来的幼崽小。欧盟新食品与加工咨询委员会评估了叶尔马科娃的转基因喂养小白鼠试验报告，认为该报告没有提供包括对测试饮食的足够的营养组份方面的信息。啮齿动物饲喂大量大豆原材料时会由于遭受各种营养不平衡而导致生长速度降低及其他不利效果，确保饮食包含高度均衡的不同类型的大豆仔细平衡其营养成分及抗营养成分是至关重要的。但叶尔马科娃的研究忽略了这个关键环节。此外，叶尔马科娃研究所采用的转基因大豆和非转基因大豆样品通过不同的来源获得。因此，委员会认为除了测试材料的转基因与非转基因差别之外，叶尔马科娃的初步研究结果可以有其它的解释，不能得出转基因大豆对小白鼠健康有害的结论。值得一提的是，叶尔马科娃毫不掩饰她的绿色和平组织成员身份。另外一个所谓的转基因安全事件是2009年法国卡昂大学的研究团队在国际生物科学杂志上发表了三种转基因玉米品种对哺乳动物健康影响的报告。为什么说是所谓的转基因安全事件？一是因为他们并不是做了实验，而只是把孟山都公司3个转基因玉米90天大鼠喂养数据进行统计学重新分析；二是他们在致谢中明确其研究受绿色和平组织的资助，因此，该研究动机和结果可信性受到公众质疑。欧洲食品安全局转基因小组在2009年针对该工作形成了一个决议：法国卡昂大学的研究团队提供的数据不能支持作者关于转基因玉米对大鼠肾脏、肝脏造成伤害。2009年10月，欧洲食品安全局转基因生物小组按照转基因植物及相关食品和饲料风险评估指导办法及复合性状转基因植物风险评估指导办法提出的原则，对转基因抗虫和除草剂作物给予了一个权威性的科学意见：在对人类和动物健康环境影响方面，转基因与非转基因一样安全。金记者漠视新闻记者客观公正的原则，不把事件的来龙去脉交代清楚，刻意渲染不安全的数据，却只字不提事件后期权威机构的否定性意见和评价结论。金辉炮制谣言的品牌标志之二就是“断章取义”。<br />
<br />
在极力渲染转基因超级杂草和健康危害后，金记者援引2010年6月8日杜邦公司的信息：其子公司开发的“新一代转基因”大豆获得美国农业部与美国食品和药物管理局（FDA）的批准，将于 2012年上市，标志着转基因作物开发真正进入了“环境友好”和“保障健康”的发展阶段。金记者由此推断：显然美国的转基因技术已经转向，正从挑战天然和违背自然的发展思路转变到尊重天然和服从自然的框架中。在发展迅猛的转基因技术领域，不断有新技术和新产品问世是再自然不过的事了，而且新一代转基因产品能否最终占领全球市场还是一个未知数，金记者在报道中宣称“美国的转基因技术已经转向”还为时过早，尽管新一代转基因技术必将成为转基因产业发展的制高点和增长点。美国是否在全面反思转基因技术？美国国家科学院于2010年4月13日在网络媒体上发表的最新报告《转基因作物对美国农业可持续性的影响》最具说服力。该报告从农户视角对美国发展转基因作物14年来的环境、经济和社会效益作了全面和客观的分析，指出从总体情况来看，与不使用转基因技术的传统农业相比，转基因技术为美国农民创造了巨大的环境收益和经济收益。显然，美国不会全面反思，相反只会全面推进其转基因产业的发展。此外，具有抗虫和除草剂性状的第一代转基因产品仍然是全球转基因市场的主力军，2009年，25个国家种植了1.34亿公顷的转基因作物，比2008年增长了7%。美国仍然是最大的转基因作物种植国，种植面积为6400万公顷。抗虫和除草剂转基因玉米占据了美国玉米种植面积85%，抗虫转基因棉花占据了美国棉花种植面积90%。明明是在“一如既往和全面推进”，但在金记者变戏法的报道中就变成了“开始转向和全面反思”。金辉炮制谣言的品牌标志之三就是“颠倒黑白”。<br />
金记者无视国际转基因技术及其产业飞速发展的现状，无视我国政府推进转基因产业的决心，也不顾中国人多地少的基本国情，炮制“美国全面反思转基因技术”论调，依据何在？是何用心？</div>

							
							
							
							
