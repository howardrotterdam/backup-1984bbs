													<h2>美国全面反思转基因技术 医生提示避免食用</h2>
						
						
																											<div id="postmessage_727436" class="t_msgfont">美国全面反思转基因技术 医生提示避免食用<br />
2010-07-07 04:29:52　来源: 国际先驱导报　跟贴 206 条 手机看新闻 <br />
美国对转基因的全面反思始于去年，在最近一段时间高潮迭起。美国主流科学家表示：转基因作物远没有当初想像的那么美妙，更没有转基因技术公司所承诺的那么神奇。显然，美国的转基因技术已经转向，正从“挑战天然”转变到“尊重天然”和“服从自然”的框架中。<br />
<br />
2010年4月14日，国际环保组织绿色和平在北京召开新闻发布会，宣布在中国重要的水稻种植地湖南和湖北的种子市场上发现了正在违法出售的转基因水稻种子。<br />
<br />
国际先驱导报7月7日报道 “我们过去用不了一滴农药就能杀死的小草，如今被转基因转成了对所有农药都刀枪不入的‘超级大草’。”安德森是美国田纳西州西部的农民，从去年开始，他就开始为一种叫做长芒苋的杂草头疼。<br />
<br />
这种草每天可以长七八厘米，最高能长到两米多，把农作物全都盖在底下，见不到阳光。这种粗壮的杂草非常结实，收割机经常被它们打坏。<br />
<br />
不下十种“超级杂草”正在美国22个州至少上百万公顷农田中肆虐。这些农田的共同特点是，都种植了转基因作物，并且使用了孟山都(美国农业生化公司)的“农达”专利除草剂。<br />
<br />
如今，除草剂“滋养”出来的抗除草剂杂草布满了农田，农民被迫喷洒毒性更强的除草剂。遗憾的是所有的除草剂对这种超级杂草都无济于事，孟山都说开发针对这些变异杂草的除草剂还需要6年时间。<br />
<br />
农民不可能等6年，为了除草，他们想尽办法，或者干脆用手工除草，在投入几十万美元代价治草依然无效后，不少农民选择放弃。超级杂草在转基因种植区蔓延，一些耕地被迫荒芜。<br />
<br />
在美国，转基因作物正在脱下神话的外衣。美国科学家表示：转基因作物远没有当初想像的那么美妙，更没有转基因技术公司所承诺的那么神灵。<br />
<br />
医生提示避免转基因食品<br />
<br />
美国对转基因的全面反思始于去年，在最近一段时间高潮迭起。<br />
<br />
2009年5月，美国环境医学科学研究院推出的报告引起了轰动。报告强烈建议：转基因食品对病人有严重的安全威胁，号召成员医生不要让他们的病人食用转基因食品，并教育所在社区民众尽量避免食用转基因食品。<br />
<br />
“一些动物实验表明，食用转基因食品有严重损害健康的风险，包括不育，免疫问题，加速老化，胰岛素的调节和主要脏腑及胃肠系统的改变，”美国科学院环境医学研究院得出的结论是，“转基因食品和健康的不利影响之间不是了无关系的，而是存在着因果的关系。”<br />
<br />
“越来越多的医生已经开出无转基因食物的处方(给病人)。”世界著名生物学家普什帕米巴尔加在审查了600多个科学期刊后得出结论：转基因生物是令美国人健康急剧恶化的一大因素。<br />
<br />
对于转基因的的侵害原理，研究院指出：插入到转基因大豆里的基因会转移到生活在我们肠道里的细菌的DNA里面去，并继续发挥作用。这意味着吃了之后，我们虽然不吃转基因食物，在我们体内仍然不断产生有潜在危害的基因蛋白质，“说透彻一点，吃转基因玉米，会把我们的肠道细菌转变成生活着的农药制造厂，可能直至我们死为止”。<br />
<br />
报告发布后，美国卫生部2009年底也发表科学论文，指明转基因食品可对内脏造成伤害。据美国国家科学基金会的调研报告显示，多数民众对转基因持有负面态度和怀疑态度。<br />
<br />
在美国，转基因食品虽然不做标识，但是经过官方鉴定的非转基因食品，也就是天然有机食品都会打上明确标识。<br />
<br />
来自美国农业部的数据表明，美国有机食品的零售销售从1997年的36亿美元上升到2008年的2110亿元美元，尽管美国发生经济衰退，但有机市场一直蓬勃发展，并导致有机产品的定期短缺。<br />
<br />
超级虫草让政府改口<br />
<br />
健康之外，转基因作物给农业造成的麻烦也引起了学界和政府部门的关注。<br />
<br />
今年4月，美国国家科学院发布的研究报告《转基因作物对美国农业可持续性的影响》中，针对让农民苦恼的杂草问题指出，种植转基因作物并没有消除杂草，反而使除草剂用量持续上升，给环境安全造成威胁。<br />
<br />
除了超级杂草，转基因农田里还出现了超级虫。由于转基因作物并不针对次生害虫，这使得一些次生虫渐渐成为作物的主要害虫。而除虫剂让这些害虫有了抗药性，变成超级虫，农民虽然投入更多的药物治理虫害，却仍无济于事。<br />
<br />
美国国家科学院说：长期实践证明，所谓防虫害的转基因作物种植需要拿出农田的20%套种同类天然作物，以便让害虫“有饭吃”，避免它们成为抗体“超级害虫”。就是说，转基因作物不但没能防虫害、反而促使原本是小虫害的害虫变成“超级害虫”。<br />
<br />
美国国家科学院的报告用16年的实践事实和统计数据明确说明，长期种植转基因作物会给农业经济带来无法纠正弥补的副作用。<br />
<br />
一直坚持认为美国农民从种植转基因作物中获益的美国农业部也终于改口，美国农业部的最新统计承认了：种植转基因后，农田作业的燃料成本提高了一倍多、农药的用量超过了天然作物种植、种子成本也大幅度上升。<br />
<br />
<br />
最明显的博弈发生在美国司法部和孟山都等种子公司之间，今年4月，美国司法部门介入到重审孟山都等公司对转基因数据进行市场封闭的做法的合法性等案件中。这是美国司法部门首次介入转基因技术的管理事务。<br />
<br />
一直以来，转基因厂商都以专利权为由，禁止其他任何人或机构“彻底打开”他们的基因数据。不管是做检验还是搞技术改善，某人或某实验室机构要“彻底打开”某转基因数据，就必须事前获得该数据专利所有人的许可。<br />
<br />
不过这次，转基因公司低了头。在与美国政府部门的博弈中，一直处于上风的转基因公司的风头开始被削弱。<br />
<br />
新技术顺乎天然<br />
<br />
美国媒体认为，美国政府已明确提出，第一代转基因作物的种植和应用对人类健康和生态环保有严重的安全威胁，如果现在不马上改变思路和做法，转基因技术很可能就此夭折。<br />
<br />
事实上，孟山都、杜邦等种子公司早已敏感地感受到了美国官方态度的微妙变化和民众的抗拒怀疑。<br />
<br />
6月8日，杜邦公司宣布：其子公司开发的“新一代转基因”大豆获得美国农业部与美国食品和药物管理局(FDA)的批准，并大约将于2012年上市。该公司布告说：这是一个“里程碑”发展，标志着转基因作物开发真正进入了“环境友好”和“保障健康”的发展阶段。<br />
<br />
随后，孟山都也做出反应，表示将推出“新一代转基因”大豆等作物。<br />
<br />
据了解，“新一代转基因”的思路与第一代转基因的发展思路截然不同，它不植入外来基因，而是关闭或者抑制某些可能会产生问题的基因。“新一代转基因”采用的是基因沉默技术。<br />
<br />
美国学界以一个比方说明这种基因沉默技术：假如父母有某种遗传病，通过基因沉默技术，让那些疾病的遗传基因在孩子体内处于“关闭”或“沉默”状态，由此达到保健目的，而那样做，并不改变人体原本天然的基因结构。<br />
<br />
显然，美国的转基因技术已经转向，正从挑战天然和违背自然的发展思路转变到“尊重天然”和“服从自然”的框架中。<br />
<br />
质疑依然不断<br />
<br />
不过，对于新一代转基因技术，美国舆论并不是全然支持。<br />
<br />
《纽约时报》在6月12日和20日发表的述评和社论说：十多年的转基因技术产品开发大体无效、没能实现承诺的教训说明，人类还没有完全了解基因功能。即便了解，也未必能开发出利于人类健康和生态环保的技术产品和处理手段。<br />
<br />
云南财经大学社会与经济行为研究中心顾秀林也不看好新一代转基因技术。她认为生命是自然演化的，不管是“基因插入”还是“基因沉默”，都是干扰生命的微观结构和功能，都违反了真正的生命科学。生命体内所有的基因都处在永远的互动过程中，强化或者弱化它们，后果都是不可预知的。“转基因这条技术路线从根本上就错了，在一条错误的路线上拐个弯继续走，不可能纠正路线上的错误”。<br />
<br />
“从长期的发展角度看，转基因技术或许最终能给人类带来福利，但谁也不敢和不能保证那一定会发生。”《纽约时报》说。<br />
<br />
美国政府为何力挺转基因技术<br />
<br />
美国政府始终有个底线，严防转基因作物侵入现有的天然农业生产系统和天然食品供应系统<br />
<br />
《国际先驱导报》记者金微、直言了发自北京、纽约&nbsp;&nbsp;虽然美国政府部门已经认定，老一代转基因作物有很多害处。但是，从美国农业部批准“新一代转基因”作物的行动看，美国政府并不想让转基因作物就此消失，美国对外输出老一代转基因技术的步伐也并未改变。<br />
<br />
这或许是出于经济目的，或许是出于战略考虑，但是，掌握转基因技术的种子公司孟山都的幕后推动作用绝对不可小觑。<br />
<br />
孟山都的“无间道”<br />
<br />
美国政府对于转基因的态度和与孟山都的关系在上个世纪就已经确定。<br />
<br />
上世纪90年代初，美国食品和药物管理局(FDA)对转基因食品安全问题发出警告：转基因食品有内在的危险，并可能制造出难以检测的过敏、新的疾病和营养问题。它敦促美国政府相关部门做严格的长期测试。但是，白宫下令该机构促进生物技术发展。<br />
<br />
最终，FDA招募了孟山都的前律师迈克尔・泰勒来负责研究出台转基因的政策。这一政策出台后实行至今，政策宣布，任何转基因生物安全性的研究都是不需要的，食品的安全与否全由孟山都和其它生物技术公司来决定，泰勒后来成为孟山都的副总裁。<br />
<br />
1992年，美国总统老布什宣布，转基因食品与天然食品实质相同。此后，各届总统的态度都是支持转基因作物开发，奥巴马也不例外。<br />
<br />
奥巴马上任后任命孟山都公司的说客伊斯兰・西迪基担任白宫农业贸易代表，受到美国社会的猛烈抨击。就此，《纽约时报》发表社论说：谁是我们的谈判代表？谁才可以真正代表美国农业的广泛意见？<br />
<br />
来自首相的警告电话<br />
<br />
孟山都的巨大的影响通过美国渗透到了其他国家。<br />
<br />
英国科学家普兹泰在生物技术领域工作35年，是转基因方面的顶级专家。上个世纪90年代，爱尔兰农业部门委托他进行一项转基因土豆的研究，孟山都公司称其天然无污染，还防虫害。但普兹泰博士用转基因土豆喂养小白鼠110天后，得出结论令他深感不安：食用转基因土豆的老鼠个头比普通老鼠小得多，更让人担心的是，老鼠的肝脏和心脏甚至脑部都比正常老鼠小，免疫系统更加脆弱。<br />
<br />
1998年8月中旬的一个星期日，普兹泰的研究成果在在收视率很高的英国独立电视台《行动中的世界》中播出，普兹泰说了真话，他说自己不会去吃转基因的食品，虽然他没敢说出实验小白鼠的脑子也变小了，但却加上了下面的一句话：“作为长期从事这一领域研究的科学家，我认为把人类当作小白鼠一样来做实验是非常非常不公平的。我们应该到实验室去找小白鼠。”<br />
<br />
节目播出后，普兹泰所在的研究所发布了表扬普兹泰的新闻通稿，但通稿刚发布，普兹泰博士和同在一个研究所供职的妻子却接到通知，要他们赶紧卷铺盖走人。多年后，普兹泰的老同事告诉他，电视播出后，研究所所长两次接到布莱尔的电话，一定要让普兹泰闭嘴。《粮食危机》一书透露，布莱尔之所以这样做则是因为接到美国总统克林顿的警告电话，而他的压力则来自于孟山都的一个电话。<br />
<br />
不过，真相不是孟山都能够掩盖的。1999年2月，来自13个国家的30位顶级科学家联名写了一封公开信支持普兹泰，并在英国的《卫报》上发表，引发围绕转基因农作物安全性和普兹泰小白鼠实验的论战。《柳叶刀》杂志也在1999年10月顶住政府主管机构的压力和威胁，发表了普兹泰的实验报告。<br />
<br />
俄罗斯高调反击<br />
<br />
随着时间推移，越来越多的质疑和反思正在出现。<br />
<br />
2004年，时值首个转基因作物(西红柿)获准种植上市十周年之际，美国对转基因提出第一次反省。美国国家科学院当时发布报告说，转基因作物的种植和食用对人类健康、动物健康和生态环保已经造成危害或具有严重潜在的安全威胁，而目前、人类还没有足够科技知识能力预见、防治和弥补那些威胁或危害。<br />
<br />
美国国家科学院的报告列举了世界各地转基因出现异常的试验报告：食用了转基因玉米的猪，在美国中西部农场出现假孕或不育；食用了转基因玉米饲料的母牛，在德国实验农场非正常死亡；食用转基因饲料的鸡的死亡率比使用自然饲料的死亡率高出两倍；英国市场出现转基因大豆食品后，居民的过敏症上升了50%，巴西出现同样状况……<br />
<br />
美国国家科学院的报告引起世界反转基因连锁反应，2005年，俄罗斯公布了具有世界轰动性的转基因喂养小白鼠的试验报告。<br />
<br />
这项试验由俄罗斯著名生物学家伊丽娜・叶尔马科娃主持，她发现转基因食品影响小白鼠及后代的健康：在小白鼠交配前两周以及在它怀孕期间，喂食经过遗传基因改良的大豆，一半以上的小白鼠刚出生后就很快死亡，幸存的40%生长发育也非常迟缓，它们的身体都比那些没有吃这些大豆的小白鼠所生下来的幼崽小。吃过这种的大豆的一些母鼠甚至不再有母性本能。<br />
<br />
叶尔马科娃认为，一些西方的大型跨国公司正将俄作为转基因产品的实验场。在俄罗斯加入世界贸易组织谈判中，美国就直言不讳地向俄罗斯提出了开放转基因食品市场的要求。她坚信，只要转基因产品无害性还未被证明，那包括俄罗斯在内的世界各国所面临的风险就会增加。<br />
<br />
2006年，叶尔马科娃走马上任，当选为俄罗斯国家基因安全研究会副主席，俄罗斯拒绝向国民提供任何转基因食品，严控转基因食品的进入。<br />
<br />
云南财经大学社会与经济行为研究中心教授顾秀林介绍：欧洲科学家早就知道转基因食品对哺乳动物的危害，但是作为非主流，他们能做到的只是呼吁，因为孟山都的压力无处不在。不过，在此问题上，欧洲政府正与美国“打太极拳”，等待时机。俄罗斯高调反对后，欧洲感到时机已到，一场反转基因的风暴正在欧洲酝酿。<br />
<br />
美国的底线<br />
<br />
世界各国反转基因的浪潮进一步影响美国，2008年，美国科学家证实了长时间喂食转基因玉米的小白鼠免疫系统会受到损害，该研究成果发表在同年《农业与食品化学》杂志上。同年4月，美国政府主管食品药品的部门FDA宣布撤消它在数年前颁布的CRY9C转基因玉米种植的工业指南，主要原因之一，就是该转基因作物对人类健康安全有严重威胁。<br />
<br />
此后，质疑转基因食品危害的美国政府机构越来越多。不过，即便反对声势浩大，美国政府仍不打算放弃发展转基因作物，并继续支持转基因种子公司向其它国家输出技术。<br />
<br />
值得关注的是，不管如何支持转基因作物开发，美国政府始终有个底线：严防转基因作物侵入现有的天然农业生产系统和天然食品供应系统。美国的天然农田面积远远超过转基因农田面积，而且，转基因农田大部分都是远离天然农业和与世隔绝的新开垦田地。美国法规保障美国有足够质量和数量的天然农田战略储备，即：一旦造成巨大危害而不得不放弃转基因作物和现用农田，美国还有足够的天然农田养活全体美国居民。<br />
<br />
链接：转基因在中国<br />
<br />
当美国正在对转基因技术进行第二次反思，并且找到了新的发展方向时，中国依然在为老一代转基因技术的安全性争论不休。<br />
<br />
<br />
自去年年底，农业部给两种转基因水稻颁发了安全证书后，巨大的争议就一直没有彻底平息。一方面，证书的获得者，华中农业大学作物遗传改良国家重点实验室负责人张启发和他的团队称，转基因作物是安全的。还有科学界人士称，转基因可以破解粮食安全问题。另一方面，很多科学家和学者举出国际上大量的实验来证明，转基因主粮如果商用是多么可怕。<br />
<br />
争论尚未有结果，针对质疑，农业部说，颁发安全证书不等于商用，农业部从未批准转基因粮食种子的商业化。不过，农业部的声明未能安抚反对者的焦虑，因为种子管理的漏洞依然存在。<br />
<br />
如今，关于转基因安全性的争论仍在继续。也许，美国的经验会给我们一些启示。<br />
<br />
(本文来源：国际先驱导报 )</div>

							
							
							
							
