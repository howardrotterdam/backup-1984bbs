萧瀚北风等主张公盟交罚款的人都是左派，不是自由主义者<br />
<br />
&nbsp; &nbsp; 我认为左派有两个特点，第一是反对自由，不尊重他人的个人选择。具体表现在于这些人以种种理由否认许志永在其文章中公开声明的“宁愿坐牢也不交罚款”是许志永真实意愿的表达。<br />
&nbsp; &nbsp; 左派的第二个特点是以主观的善良愿望取代客观现实；做事只为了证明自己的良心，而不考虑实际效果，用英文来说这叫做wishful thinking。比如说，最低工资政策实际上对穷人不利，但是左派仍然支持该政策，他们这样做其实只是为了证明自己是同情穷人的有良心的人，而不是为了实际对穷人有所帮助。在公盟事件中，这种倾向最突出的表现，是北风所说的：<br />
&nbsp; &nbsp; “向公盟捐款这本身也是一项非常好彰显公民力量的一种方式，你说明你自己参与了，然后你自己也希望看到一个好的效果，所以，在这方面我并不能如果捐款了但又救不出来怎么办，我们明知世界有很多不可为而为之，即事实我曾努力成败不必在我，你有一份良心有责任要做一件事，但是做到做不到，还要靠客观条件的制约，所以，我觉得用结果来展现行为是完全不可取的行为。”<br />
&nbsp; &nbsp; 还有顶顶牛网友所说的：<br />
&nbsp; &nbsp; “对这事的态度可以分成两类：1.希望用钱把许志永赎回来的...如跑到北韩救人给小金让步/好处的克林顿一类；2.希望许志永要么成为人肉炸弹,要么永远退出政坛/民运圈子...的哈马斯派。”<br />
&nbsp; &nbsp; 从以上两段引文中我们可以看出，左派要么是认为只要我们做了，自己良心上过得去，结果如何并不重要；要么就是分不清希望和现实，觉得自己的美好愿望一定能变成现实，或者是认为坏事的发生是报告坏消息者的恶意造成的，暴雨是气象预报员造成的，等等。<br />
&nbsp; &nbsp; 在公盟事件中，主张缴纳罚款者的美好愿望无非就是希望这样做了之后许志永能够免于坐牢，公盟还能继续存在，许志永和公盟还能继续为中国的维权事业工作。至于缴纳罚款实际会带来什么后果，是否会带来他们想要的后果，他们就不屑于考虑了。本人将对缴纳罚款的可能后果进行分析。<br />
&nbsp; &nbsp; 1.主张缴纳罚款者过于天真的愿望是不可能实现的。政府不是索马里海盗，政府拘留许志永的目的不是要勒索赎金，而是要消灭公盟，打击维权。因此此事件最大的可能性是即使公盟全额缴纳税金和罚款，政府仍然会对许志永判以徒刑，并且找借口取缔公盟（具体的法律问题请杨支柱老师来阐述）。在这种情况下，缴纳罚款简直是脑残之极。<br />
&nbsp; &nbsp; 2.许志永如果想要快获得自由，除了需要缴纳罚款之外，恐怕还需要亲自解散公盟并且向政府保证今后不再从事维权和政治活动。这种选择意味着许志永要放弃多年来的努力和自己的政治理想。如果缴纳一百多万的罚款就为了换得这么一个结果，在我看来也是脑残之极。而且即使许志永愿意做出这种屈辱的选择，政府是不是放他一马也要看自己的心情如何。<br />
&nbsp; &nbsp; 3.有人说缴纳罚款之后许志永大概能够少判两年。别的不说，某个曾经坐过牢的朋友告诉我，现在监狱里花钱减刑，5000块就能减一年。一百多万就为了减几年刑，这交易实在太不划算，还不如直接把这一百多万给许志永和庄璐更划算点。<br />
&nbsp; &nbsp; 4.缴纳罚款让许志永本人陷入被动。监狱内外的互动和配合中，较好的做法是监狱外面的家人、同志和营救者保持一定程度的强硬姿态，这样狱中的人无论选择坚挺还是妥协，都会有较多的回旋余地。最好是外面的人演白脸，狱中人演红脸，这样许即使选择妥协，也仍然能够成为英雄，出来之后还能继续做事，不至于满盘皆输。相反，按照公盟现在的做法，外面的同志首先选择妥协投降了，许志永本人如果要选择坚贞不屈，就要面临更大的压力；而如果许要选择妥协，就只能配合自己的同志，牺牲自己的名节和将来继续做事的机会企求当局开恩了。<br />
&nbsp; &nbsp; 5.公盟缴纳罚款让其他NGO陷入被动。公盟用捐款缴纳罚款之后，如果政府再用同样的办法来对付其他NGO，其他的NGO该怎么办？继续向社会募捐来缴纳罚款？我们有多少资源经得起这样浪费？而且公盟开了缴纳罚款的先例之后，其他NGO如果拒绝缴纳罚款，势必要面临更严厉的制裁。这就是为什么美国政府决不向恐怖分子投降的原因：支付赎金换得人质获释，只会鼓励恐怖分子今后绑架更多的人质。<br />
&nbsp; &nbsp; 6.北风说，如果公盟补了税款政府仍然不放人，更可彰显当局不义。在我看来政府拘留许志永本身就证明了它的不义，用不着再花一百多万来证明这一点。<br />
&nbsp; &nbsp; 7.北风说捐款可以体现公民力量与意志，也有人说给公盟捐款才觉得自己参与了。这就是我前面说的做事只为了证明自己的良心，而不考虑实际效果。不过即使这样，我仍然不打算反对这种做法。我不反对给公盟捐款表达支持，但是坚决反对公盟缴纳罚款。建议大家每人给公盟捐一块钱，亲自到公盟办公室去捐，一方面显示我们支持公盟，给我党施加压力，一方面表达我们反对用捐款交罚款，给公盟施加压力。</div>

							
							
							
							
