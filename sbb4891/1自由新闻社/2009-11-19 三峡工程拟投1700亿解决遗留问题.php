<br />
　　当初修建三峡是为了防洪和发电，那么现在电虽然发出来了，然而它却给航运、地质、移民、气候、环境等带来巨大的负面影响。<br />
<br />
　　2008年发生在四川的512大地震，有人质疑这可能与三峡大坝对地质的破坏有关。<br />
<br />
　　同时，在德国的水利工程专家王维洛在就三峡问题接受本台的采访时也曾表示，三峡大坝两次蓄水，但却两度“踩煞车”。<br />
<br />
