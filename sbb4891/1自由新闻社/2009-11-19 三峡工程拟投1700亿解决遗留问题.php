　　为应付“后三峡”时代到来，中国政府需要在投资1700亿元人民币<br />
<br />
　　据中国的媒体报道，为解决“后三峡”时代的移民安置以及地质灾害，还需要大约1700亿元人民币的投资。<br />
<br />
　　但中国的一位专家表示，后三峡时代的总开支肯定会超过3000亿元。<br />
<br />
　　“钓鱼工程”<br />
<br />
　　三峡工程从一开始论证之初就一直具有争议。而三峡的开支也象滚雪球一样越来越大，而且似乎还在不断的增加。<br />
<br />
　　多年来一直关注三峡工程的北京政论人士戴晴说，中国官方目前承认的数字是1800亿，但中国内部承认已经花了6000亿，然而因为后来的灾害不断的发生，其实花费的可能已经不止10000亿了。<br />
<br />
　　戴晴说，一些对三峡工程持不同意见的专家和科学家们早就明确指出三峡是一个“钓鱼工程”。<br />
<br />
　　它从最初向上报的一个很小的数字，到后来不断的追加钱，没完没了！<br />
<br />
　　钱从哪来？<br />
<br />
　　当初中国政府和一些支持三峡上马的人士认为，他们可以从世界银行、亚洲银行等地筹到贷款，但当这一愿望落空后，只好依靠国家拨款。<br />
<br />
　　但即使这样仍然不够，因此从1990年代中期开始，中国通过了一个三峡用电基金，即用电费的方式，也就是说家家户户所交的电费中已经涵盖了支持三峡的钱。<br />
<br />
　　戴晴认为，这种通过用交电费的方式来为三峡集资可以说是强征三峡建设基金。<br />
<br />
