													<h2>三峡工程拟投1700亿解决遗留问题</h2>
						
						
																											<div id="postmessage_333562" class="t_msgfont">人民日报11月19日报道 “后三峡”时代来临，我国将如何求解“后三峡”问题?解题路径即将出炉。国务院三峡工程建设委员会办公室目前正在组织编制、完善《三峡工程后续工作总体规划》，11月底有望报国务院审批。<br />
<br />
受三峡办委托，由水利部长江水利委员会牵头制定《三峡工程后续工作总体规划》。10月完成了各专项规划编制，并接受了中国国际工程咨询公司的评估，目前正在进一步修改。<br />
<br />
规划涉及长江中下游<br />
<br />
2008年9月，国务院三峡工程建设委员会第十六次会议对三峡工程后续工作进行了部署，确定由国务院三峡办，会同国家发改委、财政部研究提出三峡工程后续工作方案，报国务院审批。<br />
<br />
2008年10月，国务院三峡办组织有关单位开始编制三峡工程后续工作规划大纲。2009年3月，《三峡工程后续工作规划大纲》送审稿出炉。3月26日，三峡办召开三峡工程后续工作规划编制工作会议，全面动员和具体部署规划编制工作。<br />
<br />
三峡办副主任卢纯在这次会议上透露，三峡工程后续工作规划范围，按地域分为三峡库区和长江中下游影响区。<br />
<br />
三峡库区是指水库淹没及工程建设涉及的湖北省、重庆市的20个区县;长江中下游影响区是指三峡工程建成后，由于水文条件变化对长江中下游干流两岸、洞庭湖区和鄱阳湖区、河口区可能造成影响的区域，主要包括湖南省、江西省、江苏省和上海市。<br />
<br />
将解决诸多遗留问题<br />
<br />
重庆市和湖北省认为，三峡工程建设期间，库区的各项治理投入远远不够，诸多遗留问题因经费不足且未能妥善解决，对“后三峡投资清单”，后续规划将作出怎样的安排?一直以来这成为外界特别关心的一个问题。<br />
<br />
早在今年“两会”期间，重庆市副市长谭栖伟曾透露，经初步测算，仅重庆要解决移民搬迁安置、安稳致富及地质灾害防治工作等诸多问题，还需要大约1628亿元的投资。<br />
<br />
今年8月，一位专家在接受媒体采访时说：“现在重庆各区县都在拟订非常庞大的规划预算，今后的资金肯定还不止这些，如果加上三峡库区湖北区域的投资，后三峡时代的投入肯定会超过3000亿元!”<br />
<br />
长江委长江流域水资源保护局原局长翁立达此次参与了《三峡工程后续工作总体规划》的编制，负责“三峡水库综合调度与综合效益拓展”专题研究内容。他透露，目前，三峡工程后续投资规划初步框定在1700多亿元。<br />
<br />
翁立达称，在1700多亿元的后续资金分配中，大头在移民安稳致富上。该说法得到了我国知名水利水电专家、中科院院士陈祖煜的证实。<br />
<br />
陈祖煜还透露，当年三峡工程主要是在宜昌修坝，很大一部分投资发生在湖北，这次后续投资很大一部分将发生在重庆。<br />
<br />
