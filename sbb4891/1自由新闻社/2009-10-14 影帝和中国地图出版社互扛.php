													<h2>政治华北和地理华北</h2>
						
						
																											<div id="postmessage_283486" class="t_msgfont">吾讲斯美<br />
<br />
话说老农作为有特殊贡献的猪草专家，由中组部安排，正在北戴河享受双倍年假。有一阵没上网了，今天借招待所的机器上来看看，顿时被某报文章雷倒！雷得倒老农的，自然是大问题，俺也只能打破诺言，上来写几句，多少写几句。倒也巧，内容正合每月第二个周五的毁人不倦谈教育；不过，作为本月第一篇，或许该算吴说八道杂拌儿。<br />
<br />
　　文章题目叫作《谁对教材错误视而不见》，评论的是“９月６日，宗理在北京３５中学听课时指出了地理教材中的错误――该校选用的地理教科书把陕西、甘肃划入华北地区。北京市中学地理特级教师张凯接受记者采访时明确表示，把陕西、甘肃划入华北地区不对，尤其是将甘肃划入华北很荒唐，因为从地域、地理、经济等方面来看，甘肃都跟华北比较远，应该划入西北或者西部。（新花网９月１０日）”<br />
<br />
　　老农大吃一惊，立即打电话给国务苑办公室的朋友。朋友也被雷倒。朋友当时全程陪同，据他说，宗理是觉得课本应该指出“华北”这一说法的不同意义。宗理的原话，大致是这样的：<br />
<br />
　　“华北”作为地理名词，确实包括了陕西和甘肃（至少是甘肃的大部分）。但是，在延安时期，为适应各解放区的分散状况，曾将全国划分为六大地区（东北，华北，西北，华东，中南，西南），每一地区派出撑指局分局，就地加强领导。陕西和甘肃划归西北局领导，在这一意义上的政治华北，不包括陕西和甘肃两省。这一作法延用到４９年之后，比如邓晓平同志五十年代初曾任西南局书纪。紊革中，毛择东同志为加强重阳一元化领导，撤销了撑指局六分局，紊革后也没恢复。但六大地区的政治划分仍然存在，比如，陕西和甘肃两省是归兰州军区管，而不是北京军区。<br />
<br />
　　喜欢引用古诗的宗理，这回引用了民间信天游。宗理笑着说，他在西北工作时，听老乡唱信天游《兰花花》：“一十三省的女儿哎，数上咱蓝花花好哟。”这 “一十三省”号称“南七北六”，“北六省”（直隶即河北，河南，山东，山西，陕西，甘肃）指得就是传统意义上的华北，从清代以来就如此用。但在政治上，比如人大开会，分大组讨论时，陕西和甘肃的代表分在西北组。<br />
<br />
　　宗理给同学们上了一堂生动的政治、历史和地理多元综合知识课。他的渊博学识和文学修养，令老师和同学赞叹不已。<br />
<br />
　　某报那篇文章说：“上述地理教材是‘从１９９５年秋季起，北京市部分区县的三年制初级中学开始使用’的，这意味着，在宗理亲自听课并指出这一错误之前，该套地理教材实际上已在北京使用整整１４年了。一个明显甚至是‘很荒唐’的教材错误，却长期没有得到揭露、纠正，直到日理万机的宗理偶然间发现、指出，显然是一件极为尴尬之事――既是教材编撰的尴尬和遗憾，亦是教育本身的尴尬和遗憾。”<br />
<br />
　　My Dear 童鞋啊，一套专家编的、用了十四年的教材，当然可能有常识错误，但几率并不大。所以，当你认为自己发现“错误”时，一定要拿出“注经”工夫，认真查核一番。老农请朋友抄来三段《辞海》（１９９９版，北戴河到处是休假官员但到处找不到一本《辞海》），请读者大人明鉴其地理意义。<br />
<br />
华北　①自然地区名。包括辽东、山东低山丘陵，华北平原及辽西下游平原，黄土高原和冀北山地等四个自然地理单元。（下略）<br />
<br />
黄土高原　……包括山西省全部，陕西省北部，甘肃省中部、东部及宁夏回族自治区东南部和河南省西部。（下略）<br />
<br />
西北　①自然地区名。包括天山、阿尔泰山、昆仑山、阿尔金山、祁连山、塔里木盆地、准噶尔盆地、阿拉善高原、河西走廊等地理单元。（下略）<br />
<br />
　　如果将陕西和甘肃归入西北，岂不是把黄土高原这一自然地理单元划为两半？<br />
<br />
　　呵呵，俺在本专栏文章《在大学时习中学》里说，“不要说英语，就连那些很不受重视的中学知识，比如地理，都可以有意料不到的大用处。西方媒体的中国报道，什么方面问题最多？以老农所见，地理也”，现在你们相信了吧？<br />
<br />
　　俺还说，“只是记得一点中学地理知识啊，就可以混进《环仇时报》当准公务员，还可以不加惊动地结交漂亮女生。一个平常男人的两大梦想，当公务员，娶漂亮妞，大学还没毕业就已经实现了”。《在大学时习中学》才贴上网，老农又见到一个例证。<br />
<br />
　　一位女记者去阿富汗采访选举事项，她的报道很有教益，让老农知道了不少事情。她说在喀布尔能望到天山，山那边就是中国。Well，天山横贯西域，将西域分为南北两部分。南北向分隔中国和巴基斯坦、阿富汗的是喀喇昆仑山――这才是她在喀布尔看到的。<br />
<br />
　　女记者是自己人，错了就错了，无所谓。但《纽约时报》记者也是人，而且还不是中国人，他们也会犯中国地理错误。如果上述错误是《纽约时报》记者犯的呢？<br />
<br />
　　你就扑过去一把揪住，上纲上线大批判！<br />
<br />
　　天山南北自古以来就是伟大祖国神圣不可分割的领土，《纽约时报》居然说翻过天山就是中国，充分暴露了他们企图分裂我国的罪恶用心！（机关枪声）哒哒哒哒哒哒……《纽约时报》猖狂反华，决无好下场！（机关枪声）哒哒哒哒哒哒……美帝国主义亡我之心不死，我们爱国青年决不让敌人的阴谋得逞！（机关枪声）哒哒哒哒哒哒……<br />
<br />
　　这么几梭子扫下来，《环球施暴》火光一片，今年宣战部“五个一”大奖，肯定有你一份。省委书纪一看给省里争光了，特批住房一套，立即破格提拔为处级干部。有地位，有房子，就会有漂亮ＭＭ哭着喊着非要嫁给你。一个平常男人的两大梦想，当公务员，娶漂亮妞，你二十才出头就已经实现了。今后前途无量。凭什么？就凭记得一点中学地理知识。<br />
<br />
　　昨天有同学在《在大学时习中学》一文后留言――老农很高兴，这篇文章还在吸引点击――“老师你不明白，很想撕，不得不撕。或许是老师豁达，我辈羸弱。”老农也不是豁达，老农只是觉得发泄不会有实际效果。或许，书中自有黄金屋，书中自有颜如玉。撕课本，是你的自由；但你不知道的是，你或许撕掉了书纪特批的房子及随房子而来的婆姨。</div>

							
							
							
							
