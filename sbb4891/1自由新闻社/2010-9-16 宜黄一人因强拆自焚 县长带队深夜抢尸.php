现场 姐妹坚守厕所等记者<br />
<br />
9月10日，江西省抚州市宜黄县的钟如奎一家因拆迁矛盾与拆迁人员发生冲突。有报道称，钟如琴（钟如奎妹妹）、罗志凤(钟如奎母亲)、叶忠诚(钟如奎大伯)三人是自焚烧伤。但是宜黄县人民政府办公室于9月12日在其官网发布《关于“宜黄县一拆迁对象泼洒汽油不慎烧伤”的事实情况》，称事件并非自焚，而是不慎将自己点燃。目前伤者仍在救治，事件还在调查处理中。<br />
<br />
“不慎烧伤”事件发生时，钟如翠和钟如九也在现场。钟如翠是钟家老四，33岁，在南昌当瑜伽教练。钟如九，1988年出生，钟家老九，在南昌做服装销售员。昨天，这姐妹俩成为“不慎烧伤”事件续集的主角。<br />
<br />
昨天下午，快报记者联系上了钟如翠。她的声音充满了疲惫，但仍坚持向记者讲述了让她惊恐无助的一个上午。钟如翠说，前天她们买了去北京的机票，起飞时间是昨天早上8点多。她们早上7点就来到昌北机场，然而当她们坐电梯来到安检入口时，突然涌过来至少十几人，将她们控制住。钟如翠如此描述当时的情况：“将我们抓住了往外面拖，我们犯了什么法？机场这么多人他们也敢这么做。”<br />
<br />
“很多人是拆迁那天我见过的，有房管局的，还有县委县政府的领导。”钟如翠说，没想到他们竟然早就在机场等着。“在拉扯过程中，我妹妹因为又急又怕晕了过去，现场经医生急救才醒过来。我跪在安检处让机场的警察救救我，他们这才放手，但是要求我们去机场派出所的办公室谈一谈。”<br />
<br />
钟如翠说自己不愿去，只想去北京，但对方称民航大检查，飞机哪都去不了。两姐妹僵持在安检处，后因为安检处人多而被要求往外走。“一边走我就问能不能上厕所，他们说可以，然后我们就走进厕所锁上了门。”<br />
<br />
进了厕所，姐妹俩第一时间给报道过此事的《南方都市报》记者打电话，然后又打电话报警，不管外面如何叫门，就是不开。“因为我不知道他们要带我们去哪里，要把我们怎么样。”钟如翠说，在厕所的时间起码有40多分钟，这段时间里她们俩非常恐惧和无助，直到有记者赶到机场，姐妹俩才终于从厕所出来。随后，她们跟宜黄县县长刘文波坐在一起谈判。钟如翠告诉快报记者：“他们告诉我不要炒作，不要被利用，最后说明天跟抚州市的领导谈一下。”<br />
<br />
直播 网友实时关注事件进展<br />
<br />
“正在听一位专家谈网络的重要性：上访不如上网。”昨天上午11:15，网友陈彤发了这样一条微博。陈突发此言，被网友认为话有所指。因为昨天上午8点多至11点多，《凤凰周刊》记者邓飞在微博直播南昌昌北机场的“女厕攻防战”，使事件得到了全国网友实时性的关注。<br />
<br />
昨天上午8：11，《凤凰周刊》记者邓飞在微博上转发网友的微博，“今天上午7点，抚州自焚事件伤者钟家的两个女儿在南昌昌北机场，欲买机票去北京，被宜黄当地四十多个人控制在机场，家属报警无用，现仍在机场，处于被扣状态中，泣血求助网友。”<br />
<br />
随后的3个小时里，邓飞一共发了20多条微博，其中9条以“昌北机场女厕攻防战直播”为标题，实时直播了昌北机场的情景。钟如翠姐妹如何遇到“围攻”，如何退入厕所坚守，以及“攻守”双方的个人简介全部呈现在网友面前。<br />
<br />
昨天快报记者打通了邓飞的电话，他说自己当时不在现场，微博直播的信息来源于其他媒体记者、网友和钟如翠的通话，大家将自己得到的信息都聚集在一起，邓飞将这些信息实时发布在网上。邓飞的微博直播立即成为网友关注的焦点，每条微博都被数百上千次转播，钟家姐妹的一举一动牵动着万千网友的心，还有网友将微博直播的内容转至天涯、猫扑等网站。<br />
<br />
邓飞在其中一条微博直播中说：“现在宜黄县果然调来并出动了女工作人员，她们拼命在敲钟家女儿藏身的厕所门，要求钟家人‘出去谈’，钟家女儿将门扣住不敢开。她们现在极度紧张，生于1988年的钟如九在电话里几乎说不出话来。”<br />
<br />
邓飞估计，昨天上午他的微博直播得到了上百万网友的关注，最后网站给其打电话，称“各方压力太大”，希望将微博删除。昨天下午，邓飞关于“女厕攻防战”的微博被全部删除。但让他们欣慰的是，微博直播也促成了钟家姐妹跟宜黄县政府坐下谈如何处理。<br />
<br />
新闻回顾<br />
<br />
9月12日，一则关于江西宜黄县强拆钉子户爆发冲突，钟家3人自焚的新闻报道盛传于网络。12日下午，宜黄县人民政府办公室在宜黄县官网 “中国 宜黄”上发布“关于‘ 宜黄县一拆迁对象泼洒汽油不慎烧伤’的事实情况”，称钟家以浇灌汽油等极端方式对工作人员进行威吓，却不慎误烧伤自家3人。现代快报综合<br />
<br />
<br />
<br />
通话 姐妹靠手机厕所内传消息<br />
<br />
邓飞还在微博中说：“《保持通话》现实版上演，中国导演们，你们有剧本了。两名女子被44名官员堵在南昌昌北机场的卫生间里，靠手机和外界联系诉说她们的困境，为保卫她们的房子，现在想上京又被堵在卫生间。绝妙的纪录片题材，比古天乐和大S的《保持通话》更有价值，操起你们的摄影机吧。”<br />
<br />
快报记者在联系钟如翠时，她也说幸好当时有手机，才把自己的情况告诉外界。在保持通话的过程中，钟如翠不仅通过记者和网友，把自己状况用文字的形式告诉网友们，而且，她的声音也传给了网友。在姐妹俩躲在厕所的时候，酷6网的工作人员对钟如翠进行了连线，她简单叙述了一下昨天上午的遭遇。这段音频也被传到了网上，录音中钟如翠的声音充满了恐惧，并且几度哽咽，多次说到”谁来救救我们“。<br />
<br />
跟钟如翠的保持通话相比，宜黄县委书记邱建国则是“禁止通话”。邓飞在微博直播中提到，带队到机场拦截姐妹俩的人是宜黄县委书记邱建国，并且公布了邱建国的手机号码1390704××××。快报记者昨天上午拨打此号码，但已关机。后邓飞更正说此号码有误，并贴出新号码。但记者拨打该号码仍然关机，后记者经查询找到县委副书记王小林的电话，向其询问事件进展，对方称自己已调离，并确定尾号为88的号码是邱建国的手机号。此后记者试图联系当地有关部门采访，但电话无人接听。<br />
<br />
经历了昨天上午的“机场惊魂”，昨天下午钟如翠姐妹在南昌找了个酒店休息。钟如翠称，目前，钟家只有三个要求：一是竭尽全力抢救伤者；二是成立调查组彻查“9・10”事件，追究相关官员的责任，尤其是应当处理指挥拆迁的宜黄县副县长李某；三是伤者的医疗费用，政府应负责到底，对“截访”行为导致机票作废而产生的2000多元费用也应承担。<br />
