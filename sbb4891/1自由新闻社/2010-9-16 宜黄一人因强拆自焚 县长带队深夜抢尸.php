<br />
社论：不能让“宜黄事件”发展成违宪恶例<br />
<br />
宜黄强拆事件的恶性，到昨天已经上了一个台阶。如果说之前的强拆以及由此导致的拆迁户自焚（当地政府称之为意外自伤）还勉强算得上利益互搏的话，那么，由政府强力阻扰公民乘飞机并限制人身自由的行为，便是与民事无关了，演变成赤裸裸的践踏公民权利。（相关报道见快报今日封7版）在这条公权放纵的路上走下去，结果只能更坏：法治原则被肆意嘲弄，宪法尊严将不复存在。<br />
<br />
据媒体报道，为了达到政府既定的拆迁目标，宜黄强拆其中的一户人家动用11个单位185人。当地文件显示，为了实现对居民房屋的强制拆迁，当地政府成立了以副县长为第一副总指挥的强拆工作指挥部。参与人员包括县公安局30人、县城管局15人、县医院4人、县拆迁公司50人……当然，这是第一线的人员，想必“后方”还有法院、检察院、政府大院等一干更为强力的后援。从中可以看出，强拆已经不是土地开发商和动迁户之间的市场纠葛，而是由政府名正言顺地走到前台所主导的土地一级开发。政府和拆迁户之间的矛盾，使强拆划分出极其强势和极其弱势的双方，只要有一方不妥协，弱势的一方必败无疑。拆迁户钟家最后以三人自焚抗争的方式，就是这一力量对比下的极端反应。<br />
<br />
但是，就像“9.10”之前全国所发生的其他自焚抗议事件一样，钟家的遭遇只能以悲惨结局。地方政府对民众以命相搏的举动已从最初的紧张无措，发展到今天的冷漠应对。如果继续这种鸡蛋碰石头的辛酸游戏，不仅会造成旁观者的麻木，而且掩盖这种行为本身的警世意义。所以，钟家亲人的进京上访，是这个悲剧意义放大的唯一途径。显然，当地政府有关领导看清了这点。他们阻止“9.10”事件的升级，是担心这一事件会成为一场法治革命的活标靶。<br />
<br />
这个担心不是没有道理的。最近一段时间以来，从中央高层到社会各界，频频对一些地方政府赶在拆迁法出台之前拼命抢拆、强拆居民住房的恶劣行径，发出严厉的批评之声。近日，国土部再一次发文强调，严禁各地方政府直接插手土地开发、拆迁。在这样的情势下，更为严厉的调控措施和惩治政策可能出台，谁碰在枪口上谁就是靶子。宜黄的恐慌，在一定程度上是担心自己成为反面教材。“机场围堵战”不能不说是这种心态的应激反应。<br />
<br />
不顾民众死活、藐视法治原则、背离执政基础的暴力拆迁、强制拆迁，激起了公愤，触犯了社会的容忍底线，挑战最高的宪法原则，必须坚决予以制止，如果我们容忍和默认，宜黄事件将成为一个恶例。<br />
<br />
宜黄“9.10”事件的升级版，是这类事件走向极致的一个典型。它的危害性，在于它把宪法的尊严，置于非常脆弱的维护边界。<br />
<br />
在我国的宪法中，有关“公民基本权利”的规定，是宪法基本原则之一。公民的人身自由和财产不受侵犯，是宪法确认保护的权利。如果这一原则遭到破坏，宪法尊严将不复存在。宜黄方面对公民权利的粗暴干涉、侵犯，看似政府为公共利益越权，其实是藐视宪法威信，最终损害的是公意和国法。<br />
<br />
宪法的另一基本原则是一切权力属于人民。我国现行宪法规定，“全国人民代表大会和地方各级人民代表大会都是由民主选举产生，对人民负责，受人民监督”，“国家行政机关、审判机关、检察机关都由人民代表大会产生，对它负责，受它监督”，“人民法院、人民检察院和公安机关办理刑事案件，应当分工负责，互相配合，互相制约，以保证准确有效地执行法律”等等。反观宜黄强拆事件，所有公权力都被行政权力绑架了，既谈不上权力相互制约，更谈不上民意的监督。宪法的权力制约原则被束之高阁。<br />
<br />
宪法规定国家事务法律化、制度化，并严格依法进行管理，所有机关、组织和个人都必须严格依法办事。但是在宜黄强拆事件以及其后的机场堵人事件中，无论是违法强拆，还是违法扣人，我们看不到一点行政机关依法办事的影子，有的只是对法治精神的无视。<br />
<br />
“宜黄事件”在地方政府违宪方面的“突破”，为我们反思强拆事件屡禁不止原因提供了最典型的实例。到现在为止，除了舆论的抨击外，宜黄还没有受到来自宪政方面的任何指责。之前，也没有多少强拆事件责任官员受到法律的制裁。这不能不说是“宜黄事件”越走越远的重要背景。</div>

							
							
							
							
