&nbsp; &nbsp; 据新华社报道，2009年10月12日，在德国访问的国家副主席习近平与德国总理默克尔举行了90分钟会谈。会谈开始前，习近平向默克尔转交了江泽民撰写的有关能源和信息技术问题的两本英文版专著，并转达了江泽民同志的良好问候和祝愿。<br />
<br />
&nbsp; &nbsp; 这两本所谓的英文版专著，其一是《中国能源问题研究》，其二是《机械制造厂电能的合理使用》。前者收录了江泽民关于能源问题的两篇学术论文和一篇首次公开发表的讲话。据说，全书阐明了能源问题的重要性，分析了世界能源基本状况和发展趋势，探讨了我国能源发展面临的机遇和挑战。对此书，因笔者不知道其学术价值，所以不愿妄加评论。<br />
<br />
&nbsp; &nbsp;而《机械制造厂电能的合理使用》一书，完全不是江泽民撰写的专著，而是苏联人特莱霍夫博士的专著，江泽民只是该书的译者。而且江泽民翻译完之后，此书却没有得以出版。一直到30年后的1989年，江泽民当上中共总书记之后，虽然该书的内容是20世纪50年代的东西，早已经过时了，但因出版社没有勇气退稿，所以就出版了。<br />
<br />
&nbsp; &nbsp;于1990年笔者在北京王府井的新华书店内看到了这本书，当时该书店正在以3折的优惠价抛售此书，但仍然乏人问津。虽然是3折的优惠价，足够便宜了，但非专业人员不会买，而专业人员一看便知书中的内容因过时而成了垃圾，也不会买。所以，这本书注定没有读者。这就如同现在出版一本《论黑白电视机的维护与保养》，注定没有读者一样。<br />
<br />
&nbsp; &nbsp;令人感到匪夷所思的是，就是这本因为过时而没有读者、1990年曾经堆在北京王府井的新华书店内以3折价抛售而乏人问津的书，2008年竟然再版了，江泽民还撰写了《再版序言》；2009年与时俱进，竟然还弄出一个英文版，并由国家副主席亲自转交给德国总理默克尔，还声称是江泽民撰写的专著。这很像是愚人节的新闻，滑天下之大稽！ <br />
<br />
<br />
&nbsp; &nbsp;据此我们不只是认为江泽民爱好虚荣、爱出风头，而是我们可以据此断定，他患有老年痴呆症。因为一个在中国的权力顶峰坐过十几年的人，如果不是智力退化、思维混乱的话，他绝对不会同意把他自己几十年前被出版社退稿的、目前已无任何学术价值的东西，还要再版，并弄成英文版，既恶心中国人也恶心外国人，更恶心他自己。<br />
<br />
