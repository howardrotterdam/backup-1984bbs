													<h2>影帝国庆献礼大片上映 激情搏出位</h2>
						
						
																											<div id="postmessage_244624" class="t_msgfont">Wen Jiabao在中南海与维族农民达吾提会面<br />
9月14日，国务院总理Wen Jiabao在北京中南海总理办公室与新疆维吾尔族农民达吾提・阿西木会面。这是会面结束后，Wen Jiabao与达吾提・阿西木挥手告别。新华社记者姚大伟摄<br />
<br />
新华网北京9月16日电 这是57岁的新疆维吾尔族农民达吾提・阿西木幸福而温暖的时刻。9月14日下午5时，他又在中南海里见到自己想念已久的亲人――Wen Jiabao总理。<br />
<br />
“欢迎你，达吾提・阿西木！老妈妈身体好吗？”在总理办公室里，Wen Jiabao亲热地叫着他的名字，满脸笑意，紧紧握着他的手。达吾提・阿西木的眼角湿润了。<br />
<br />
达吾提・阿西木是新疆巴楚县琼库尔恰克乡吐格曼贝希村的村支部书记。2003年2月24日，新疆巴楚、伽师发生6.8级地震。Wen Jiabao闻讯后立即打电话给自治区党委、政府负责人，询问受灾情况，指导抗震救灾工作。3月28日，Wen Jiabao刚刚就任总理就赶往灾区考察重建工作。在地震中，达吾提・阿西木失去了5位亲人，强忍悲痛，冒着余震，逐户寻找和营救乡亲们。听到这一消息后，Wen Jiabao专门去看望他。达吾提・阿西木失去亲人时没有落泪，面对远道而来的总理时却泪流满面了。总理握着他的手说：“地震使你失去了亲人，现在全国人民是你的亲人，我也是你的亲人。”<br />
<br />
从此，总理与这位普通的维吾尔族农民结下了不解之缘。<br />
<br />
2004年2月初，当听说达吾提・阿西木进京参加“2003年感动中国十大人物”颁奖大会，并想见见自己时，Wen Jiabao当即向他发出了邀请。两人第一次在中南海里见面了。Wen Jiabao仔细地向他询问了灾后重建的情况。见面结束时，总理一直坚持把达吾提・阿西木送到中南海西北门，嘱咐他：来北京一定再来中南海。<br />
<br />
新中国成立60周年前夕，被评为“100位新中国成立以来感动中国人物”之一的达吾提・阿西木，进京参加座谈会前，向Wen Jiabao写了一份情真意切的“家书”：很长时间没和您见面了，作为您的兄弟我非常想念您！希望您在百忙中抽出一点时间见一面。<br />
<br />
两个多小时前，达吾提・阿西木得知，总理要与他见面。在大会堂参加完活动后，他赶到中南海来了。<br />
<br />
在典雅朴素、十多平方米的会见室里，生机盎然的虎皮兰、靠墙而立的书柜，熟悉的环境让达吾提・阿西木感到十分亲切，就像到家一样。这位不懂汉语的维吾尔族农民听懂了总理的问好，使劲地点头：谢谢总理，老妈妈很好！这让Wen Jiabao十分高兴。<br />
<br />
因为之前参加会议的缘故，达吾提・阿西木戴着维吾尔族小花帽，西装笔挺、皮鞋锃亮。而总理穿着一件白衬衫挽起了袖口，十分家常。Wen Jiabao热情地让他坐下，回忆起在新疆见面时的情景：“那年，我是坐火车赶到你们那里的。到你家时，70多岁的老妈妈正在烧火做饭，那时她还很悲痛。”<br />
<br />
仿佛又回到六年前的那一刻，达吾提・阿西木动情地说：“村里人都忘不了，在我们最困难的时候你去看望我们。每次到北京开会回去，大家都要问：问候总理了没有，我们现在日子一天比一天好告诉总理了没有？”<br />
<br />
Wen Jiabao说：“我也很想念他们！”<br />
<br />
在无拘无束之中，总理与达吾提・阿西木聊起了家常。<br />
<br />
“村民们都住进新房了吧？”Wen Jiabao问道。<br />
<br />
“是的，都是防震房，很安全。”达吾提・阿西木回答说。<br />
<br />
“大家日子过得怎么样？”<br />
<br />
“现在生活不错，收入有了提高，人均3000多元呢。”<br />
<br />
“村里棉田多，还是粮田多？”<br />
<br />
“种了一些小麦、玉米，还是棉田多。今年棉花丰收了。”<br />
<br />
“这是南疆的特点，日照充足，棉花好呀！村里合作医疗搞了没有？”<br />
<br />
“都参加合作医疗了。个人掏20元，国家拿80元。”<br />
<br />
Wen Jiabao很早就曾对新疆新型农村合作医疗试点工作提出过明确要求，促进了新疆农村群众看病难问题的解决。<br />
<br />
“孩子上学还交费吗？”<br />
<br />
“不但不收学费，课本费也不用交。村民们说，我们种粮食政府给补贴，盖房子政府给补助，孩子上学不用交费，看病还能报销。祖祖辈辈也没有遇到这样的好事。”<br />
<br />
从吃穿住行医疗教育，到水电路沼气，总理扳着指头一件一件事情地问，达吾提・阿西木回答得实实在在。<br />
<br />
巴楚缺水，总理一直挂念在心上，那里由上引下来的水不够，村民们挖井挖出来的水苦咸：“浅井还不行，恐怕要打深一点。国家要想办法帮助你们解决饮水问题。”<br />
<br />
“还有什么困难要告诉我？”总理诚恳地问道。<br />
<br />
达吾提・阿西木从怀里掏出了三份建议。三份建议讲了三件事情：一是加快扶贫步伐；二是发展绿色专业化农业；三是解决和群众生活密切相关的基础设施问题。<br />
<br />
Wen Jiabao细致地看完，沉思片刻说：“三件事围绕一个目的：尽快让农牧民脱贫致富奔小康。这正是我们要做的事情。去年国家已经专门出台支持新疆发展的文件，在这个基础上还要进一步加大支持力度，研究促进新疆特别是贫困地区发展的政策，改善农牧民的生产生活条件，提高他们的物质文化生活水平。”<br />
<br />
Wen Jiabao接着说：“特别要重视新疆社会事业发展，包括医疗教育文化的发展。让每个孩子都上学，不仅完成九年义务教育，而且创造条件让他们上高中、上职业学校，有条件的培养他们上大学。”<br />
<br />
总理的回答让达吾提・阿西木十分感动，他打着手势说：“这么大的国家，很多大事要总理操心呢。为了村里的小事，耽误总理时间了。”话语里带着歉意。<br />
<br />
“你说的事不是小事，是村里、乡里、县里、自治区和国家的大事情。”Wen Jiabao认真地回答道。<br />
<br />
谈起乌鲁木齐“7・5”事件，Wen Jiabao语调沉重：那是国内外“三股势力”煽动起来的打砸抢烧严重暴力犯罪事件，他们想破坏汉族维吾尔族的兄弟关系。这起事件让群众的生命财产都受到损失，我们很痛心。<br />
<br />
达吾提・阿西木说：“大家都非常痛心。这些坏人见不得我们日子过得好。”<br />
<br />
Wen Jiabao说：“在新疆，汉族离不开维吾尔族，维吾尔族离不开汉族，所有民族谁也离不开谁。我们都是一家人，应该珍惜来之不易的稳定发展好局面。现在最重要的是依法保护各族人民的生命财产安全，稳定新疆社会秩序，各族人民团结起来把新疆建设得更好。”<br />
<br />
“我要把总理的话带回去。这是我们一定要做的。”达吾提・阿西木坚定地回答道。<br />
