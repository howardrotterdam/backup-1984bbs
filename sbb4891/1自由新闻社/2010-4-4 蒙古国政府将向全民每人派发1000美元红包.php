　　蒙古人口近一半都居住在首都乌兰巴托，在首都令我们意外的是居然一个人均收入不到500美元的国家竟然到处都是世界名牌轿车，而且是原装丰田，现代，和大奔，还有劳斯莱斯，还有中国的2020依稀可见，出租车司机告诉我们说蒙古国从91年开始人民生活有了显著的提高，官方统计的GDP是500美元只是为了受援西方国家的援助而统计的，但实际情况远比这个数字要乐观得多，我们住的旅店算是当地比较有档次的宾馆了，这里的服务小姐会说四种语言而且是很精通的那一种。(蒙，俄，英，中)。<br />
<br />
