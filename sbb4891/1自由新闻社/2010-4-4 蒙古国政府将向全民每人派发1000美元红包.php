<br />
　　蒙古国的夜生活相当丰富，显然不是国人印象当中那么《静悄悄》。到处都是酒吧，餐厅和歌厅还是无夜经营那种，好多都是中国的温州和内蒙古人开的，生意相当火爆，蒙古人就餐习惯跟中国人有很大的区别，在国内好多在餐听就餐的人大部分都喜欢大声喧哗，增加气氛，可是他们不同的是就餐时很安静，没有人大声说话，吃完付账就走人，不会在吃饭上浪费时间，他们居然不会用筷子，受西方的影响都用刀子和叉子，用的比西方人还专业，呵呵。<br />
<br />
