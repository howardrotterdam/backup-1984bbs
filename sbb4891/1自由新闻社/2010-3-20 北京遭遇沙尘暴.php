													<h2>「北京下土了」　飞沙走石人人掩面　沙尘狂扫半个中国</h2>
						
						
																											<div id="postmessage_525253" class="t_msgfont">Apple Daily 苹果日报网上版 －－2010-03-21<br />
「北京下土了」　飞沙走石人人掩面<br />
沙尘狂扫半个中国<br />
<br />
 <img src="http://pic1984.12hp.de//up/1269158632.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
京津地区昨遭沙尘暴袭击，天津一名女子在街上以丝巾蒙头。路透社<br />
<br />
内地昨日遭遇近年最强风沙尘天气，西北、华北、华中、华东、西南等 15个省分被沙尘席卷，其中北京更遭沙尘暴袭击，黄尘蔽日、飞沙走石，俨成「黄城」，空气污染五级，成为今年京城「最脏」的一天。此次沙尘天气影响半个中国，累及数亿人口，是去年 1月以来，影响中国范围最大、强度最劲的一次沙尘天气。<br />
<br />
「北京下土了！」昨日凌晨，住在北京东城区的舒先生起床打开窗帘后，发出如此惊叹。只见窗外黄尘遮天蔽日，百米外不见物体；空气中弥漫尘土味道，停放一夜的私家车上铺厚厚黄土；马路、屋顶、花草树木上都附着一层黄沙；街上风力达到七、八级，行人无不遮口遮面，顶风吃力前行。<br />
<br />
有「首都后花园、温馨之都」美誉的河北廊坊及天津，同样遭到沙尘暴袭击，两个城市昨日上午笼罩于黄尘之中。除京津地区外，新疆南部、内蒙古西北部、青海、河北、山西、西、河南、山东、江苏、安徽、湖北、四川、重庆等 15省区市，几乎半个中国，昨日都受沙尘影响，其中呼和浩特、京津等地更是强沙尘暴。<br />
<br />
<img src="http://pic1984.12hp.de//up/1269158657.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
《北京》<br />
私家车蒙上一层厚厚的黄尘。新华社<br />
<br />
<img src="http://imgur.com/ssm89.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
《河北》<br />
霸州市昨沙尘挟风袭城，市民骑车难行。新华社<br />
<br />
沙尘来自蒙古　内蒙<br />
<br />
昨日是周末，大部份民众躲在家中，但沙尘对数亿民众生活仍造成影响。环保专家指，北京昨日空气质量降到五级，属重度污染，是北京今年以来「最脏的一天」。专家指，此次沙尘暴是 2006年 4月 17日一夜降 30多万吨沙尘后，四年来北京地区出现最强浮尘；同时也是去年 1月以来，影响中国范围最大、强度最强的一次沙尘天气。<br />
<br />
专家指，影响中国的沙尘源主要来自蒙古国西南部及内蒙古西北部沙漠化草原。通常每年春天，沙尘会随着北半球环流气候向东飘移，影响中国西北和华北等地，近年沙尘趋劲，越洋影响台湾、南韩、日本等地，引起这些国家地区恐慌。过去几年，内地当局试图通过在西北大面积种植「防护林」等方式阻挡蒙古沙尘，但收效成疑。<br />
<br />
昨日的沙尘暴虽未造成首都机场进出港航班受影响，但有内地网民在博客中透露，他所乘搭的航班昨晨在下降时，眼看就要落地，突然又拉高爬升，后来看到窗外一片黄沙飞舞，才明白「遇到沙尘暴，好险」！此外由于强风，昨日上午 10时左右，京广铁路石家庄到保定段有电讯铁塔被大风吹倒，刮擦到一列火车，该路段中断两小时，十几趟列车要改道或临时停车。<br />
<br />
中新社/新浪网<br />
<br />
<img src="http://pic1984.12hp.de//up/1269158682.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
<img src="http://imgur.com/xios2.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
污染恶果集中爆发<br />
<br />
<br />
内地著名民间环保组织「自然之友」前日发布《中国环境发展报告（ 2010）》。报告指， 2009年中国环境健康事件高发并非偶然，经过 30多年快速发展，环境污染危害后果正日益显现，甚至到了集中暴发时期。<br />
<br />
报告分析了去年内地多宗环保事件的影响，包括江苏盐城水污染 20万人饮水受影响、湖南浏阳镉污染 509人中毒引发群体事件、陕西凤翔 615名儿童血铅超标、湖南武冈 1,354人血铅超标、河南济源 1,088名儿童血铅超标等。<br />
<br />
这些事件曾震惊社会，引发关注。报告指， 30年来中国经济快速发展，但环保投入严重不足、环境执法不严。报告还预期环境健康事件今后若干年都有可能频发。「自然之友」发起人是中国近代名人梁启超的孙子梁从诫。<br />
<br />
中新网</div>

							
							
							
							
