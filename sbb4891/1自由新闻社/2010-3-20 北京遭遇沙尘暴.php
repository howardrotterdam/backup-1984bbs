													<h2>北京沙满天 今年最大沙尘暴横扫16省</h2>
						
						
																											<div id="postmessage_525248" class="t_msgfont">明报网上版 2010-03-21<br />
北京沙满天 今年最大沙尘暴横扫16省 2.7亿人吸尘<br />
<br />
【明报专讯】今日是农历节气「春分」，不过在华北、华中、西北等地全无春色，更遭受一股今年以来最强的沙尘暴侵袭，范围所及内地16个省区市共2.7亿人，约占全国人口的五分之一，并会到达台湾。其中，北京空气污染指数严重超标，达到「重度污染」级，民众出行大受影响。预计这场沙尘将持续至周一（22日）。<br />
<br />
据中新社报道，截至昨日11时，约有180万平方公里的国土、2.7亿人口遭沙尘天气侵扰。在北京，刚刚下完雪又「下沙」，整个城市被一片黄沙笼罩，北京气象台台长郭虎表示，前段时间的降雪确能抑制沙尘，但本次沙尘的源头在蒙古国及内蒙古中部地区，沙尘在数千米的高空，被大风吹到京城，「从气象学角度看，这是无法阻挡的气候现象。」<br />
<br />
网友调侃「要找骆驼出门」<br />
<br />
「北京欢迎你，沙尘暴埋葬你。五百年后挖出来，你就是五环上的买买提（新疆神话人物）。」北京网友「和菜头」苦中作乐，调侃自己「准备找一头骆驼出门。」新华社引述北京市民王先生说，「街上一眼看过去到处是土，尤其是停在路边的车，全部灰头土脸的。我路过洗车房还和那里的工人聊，前几天雪盖车、这几天沙埋车，你们的生意好的不得了。」在街上，汽车需要打开车头灯并慢驶，航班未受影响。<br />
<br />
昨日傍晚， 中央气象台将沙尘暴预警由蓝色上升为黄色（共分4级，蓝色为最低级），估计到今晚8时，南疆盆地、甘肃中西部、青海柴达木盆地、内蒙古西部、宁夏中北部等地有沙尘天气，风力达到5至6级，阵风风力可达7级。由于风力强劲，除上述省份外，京津地区、河北、河南、山东、、山西、陕西、四川、江苏、安徽及湖北省都会受影响。台湾行政院环境保护署亦预测，今日清晨开始至下午，将有一波沙尘影响台湾。<br />
<br />
持续至周一 台湾亦受袭<br />
<br />
北京距离内蒙古沙漠约800公里，2006年4月17日，曾遭遇来自蒙古和内蒙的总量超过30万吨的沙暴侵袭，但2007和2008年都仅有1日沙尘天气。<br />
<br />
郭虎表示，北京已进入春季沙尘多发期，预计今春沙尘将不止1天，但会少于常年平均的9.7天。预计北京今日开始转为阴天，风力下降，甚至可能下起雨夹雪。</div>

							
							
							
							
