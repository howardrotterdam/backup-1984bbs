																											<div id="postmessage_556845" class="t_msgfont">2009会计年度美国共发出113万818张绿卡（美国永久居留权）。其中，中国大陆6万4238人、台湾8038人、香港2651人获得永久居留权。<br />
<br />
　　美国国内安全部8日傍晚公布的统计报告显示，中国取得永久居留身分的人数，占所有获绿卡人数的5.7%，人数及比率都较前两个会计年度略少。<br />
<br />
　　2008会计年度中国有8万271人取得永久居留；而2007会计年度中国有7万6655人取得永久居留，这两年取得永久居留的中国人，都占总人数的7.3%。<br />
<br />
　　取得绿卡的中国新移民中，有1万1013人是根据亲属移民的优先顺序取得绿卡的；1万1295人是职业移民；2万3244人是美国公民的直系亲属及配偶；28人是抽秽移民；1万8486人是取得政治庇护身分而获得永久居留权。<br />
<br />
　　2009会计年度中，中国仍是移民美国人数第二多的国家，仅次于墨西哥的16万4920人。墨西哥移民占总人数的15%。第三名菲律宾共有6万29人获得美国绿卡。印度居第四名，有5万7304人获得美国绿卡。<br />
<br />
　　大部分取得永久居留权的人（66万7776人，占59%），是在美国调整身分，另外46万3042人来自国外。将近三分之二的人是依亲移民。<br />
<br />
　　新移民20%住在加州，13%住在纽约州，11%住在佛州，8.4%住在德州，5.2%住在新泽西州。<br />
<br />
　　2009会计年度新移民年龄中位数是31岁，全美年龄中位数是35岁。</div>

							
							
							
							
