<br />
　　多年来，中国人在美获庇护的数量一直名列榜首，基本上每年都占美国批准获庇护外国人总数的四分之一。去年美国一共批准外国人庇护2万2千多人，是十年来最少的，但中国人获得庇护的数量有增无减，比2008年增加近700人。政治庇护已成为在美没有身份的中国人获得美国永久居留权的重要途径。<br />
<br />
　　在获得庇护的6109名中国人中，有2691人是直接申请庇护获得批准，占直接获批总数的2成3；3418人是被捕的没有身份者，进入遣返程序后，通过上诉抗辩获准庇护的，占通过上诉获准庇护总人数的三分之一。<br />
<br />
　　根据美国移民法的规定，庇护是面向那些已经来到美国本土或美国入境口岸的外国人，不论其身份如何。申请人除非有紧急原因，必须在抵达美国一年内提出申请。申请人如果有犯罪记录、对美国构成国家安全威胁，或者有迫害他人的行为，将不会被批准。有身份外国人庇护申请如果不获批准，依然可保持现有身份，没有身份的外国人如果申请被否决，将被移民局遣返。<br />
<br />
　　去年获得庇护人数较多的其它国家包括埃塞俄比亚、海地、哥伦比亚、伊拉克、尼泊尔、委内瑞拉、危地马拉、俄罗斯。获得美国庇护的外国人中，80%年龄在18岁到 54岁之间，5成4是男人，4成8已婚。直接批准庇护人数最多的州依次是：加州、纽约、佛罗里达、弗吉尼亚、马里兰、华盛顿州、麻州、德州。<br />
<br />
　　去年美国还接收了主要来自战乱和重大自然灾害国家的7万4千多名难民，他们主要来自伊拉克、缅甸、不丹、伊朗、古巴、索马里。来自中国的难民很少。(余东晖) <br />
