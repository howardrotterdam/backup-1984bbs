													<h2>中国对金正日公然提出“改革开放”</h2>
						
						
																											<div id="postmessage_606560" class="t_msgfont">朝鲜日报驻北京特派记者 崔有植 (2010.05.08 10:27)<br />
<br />
北韩国防委员长金正日于5月5日下午在北京人民大会堂与中国国家主席胡锦涛举行了首脑会谈，但看其表情似乎并不高兴。在和胡锦涛握手和拥抱时短暂露出笑容后，马上又恢复严肃的表情。会谈期间他还耷拉着眼睛记笔记。双方对坐后，金正日的表情与始终面带笑容的胡锦涛和中国国家副主席习近平的表情形成鲜明的对比。在中国中央电视台（CCTV）播出的长达10多分钟的金正日访中报道画面中，金正日也是和中方人士握手时才露出微笑，但在其余时间里很难看到他特有的满面春风的样子。<br />
<br />
中国对金正日的这次来访给予了盛情款待，包括胡锦涛在内的中共中央政治局常委9名成员轮流与金正日举行双边会谈，陪同金正日视察产业设施，并进行迎接和欢送。但在会谈上中方却以前所未有的“带刺的话”来向金正日施加压力。<br />
<br />
新华社7日报道了金正日和胡锦涛的首脑会谈内容。会谈上，胡锦涛首先对迎来建交61周年的两国关系进行了一番赞美。但赞美过后，胡锦涛提出了“5点建议”，说出了心中的话。<br />
<br />
其中，第二个“加强战略沟通”的内容格外引人关注。胡锦涛说：“双方要随时和定期就两国内政外交重大问题、国际和地区形势、治党治国经验等共同关心的问题深入沟通。”<br />
<br />
胡锦涛在4年前的2006年1月曾与金正日会晤，金正日在视察中国南方城市广州、深圳、珠海、厦门等后，向中国领导层表明了经济改革意愿。但在9个月后的当年10月实施了第一次核试验。据悉，当时北韩在距离核试验几十分钟前才向中方通报，这让中国领导层愤怒不已。而去年的第二次核试验、货币改革以及今年的天安舰事件等每当发生很可能使东亚局势陷入混乱的事件时，中国在掌握北韩内部情报方面反而不如美国或韩国。<br />
<br />
据北京外交人士分析，中国一直标榜非同盟外交，禁止干涉他国内政，而如今中国公然“要求北韩通报其内政和外交上的重大问题”，是因为有这样的隐情。北京外交消息人士表示：“胡锦涛的这一发言要是放在过去可能会被视为干涉他国内政，从而引发两国间的重大问题。”<br />
<br />
中国国务院总理温家宝也在6日会见金正日时说出了近乎“忠告”的话。温家宝表示：“中方将一如既往支持朝鲜发展经济、改善民生，愿意向朝方介绍中国改革开放和建设的经验。”据北京外交界分析，介绍众所周知的中国改革开放经验，事实上就是劝北韩进行改革开放。<br />
<br />
中国领导班子此前考虑到北韩的立场，在两国领导会谈中几乎没有提到“开放”一词。即使提开放问题，也是非常注意措辞，以免给人一种针对北韩的印象。但温家宝这次打破这样的惯例正面提到了改革开放问题。<br />
<br />
金正日在此次访中期间表示：“欢迎中国企业对北韩进行投资”。而温家宝随后的发言让金正日非常尴尬。温家宝表示：“中朝经贸合作具有很大潜力，双方要加快边境基础设施建设，探讨新的合作领域和合作方式。”也就是说，北韩希望中国能马上对其进行投资，而中方却暗示首先要建设基础设施，并改善制度。<br />
<br />
据专家分析，新华社对两国领导人的对话内容没有删减地进行报道，也反映了中国与过去不一样的做法。中国对金正日自2000年以后进行的四次访问，每次都以新华社的报道文形式公开首脑会谈结果。这次也发表了2600字左右的报道文。<br />
