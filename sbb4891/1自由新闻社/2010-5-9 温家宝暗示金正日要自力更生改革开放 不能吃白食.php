核心提示：据称，金正日在访问中国期间，温家宝向金正日转达“中国无法向朝鲜提供超出联合国安理会对朝鲜制裁框架之外的援助”的立场后，金正日取消同中国领导层一同观看朝鲜版《红楼梦》，缩短日程紧急回国。日媒报道称金正日缩短访华日期是因为不满。<br />
