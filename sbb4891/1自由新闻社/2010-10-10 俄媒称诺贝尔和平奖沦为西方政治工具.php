																											<div id="postmessage_864999" class="t_msgfont">俄高官呼吁中国尊重刘晓波获奖决定<br />
作者：白桦&nbsp; &nbsp; 文章来源：VOA&nbsp; &nbsp; 点击数：354&nbsp; &nbsp; 更新时间：2010-10-10<br />
俄罗斯负责人权事务的高级官员呼吁中国尊重诺贝尔和平奖颁给刘晓波的决定。俄国官员说，中国持不同政见人士刘晓波获奖具有历史意义。<br />
<br />
中国知名持不同政见人士刘晓波获得本年度诺贝尔和平奖后，俄罗斯人权事务全权代表鲁金表示，有关诺贝尔和平奖的颁奖决定通常引起正面和负面的评价，在这方面没有一致的观点，但尽管这样，从长远来看，刘晓波这次获奖具有十分重要的历史意义。<br />
<br />
鲁金呼吁中国当局尊重诺贝尔奖委员会把和平奖颁给刘晓波的决定。鲁金说，中国持不同政见者获得这样的奖励当之无愧。<br />
<br />
*俄官员首次针对刘晓波获奖表态*<br />
<br />
鲁金还强调，他热爱和尊重中国。因此他衷心希望，中国的发展不仅应引起中国民众的自豪，同时也应获得世界其他地区人民的尊敬和肯定。<br />
<br />
