													<h2>俄媒称诺贝尔和平奖沦为西方政治工具</h2>
						
						
																											<div id="postmessage_864728" class="t_msgfont">转自性浪网：　<a href="http://news.sina.com.cn/c/2010-10-10/194821246119.shtml" target="_blank">http://news.sina.com.cn/c/2010-10-10/194821246119.shtml</a><br />
俄新社10月8日发表该社政治观察家尼古拉・特洛伊茨基的署名文章，认为诺贝尔和平奖沦为西方政治工具。以下为该篇文章的摘要：<br />
<br />
　　颁发2010年诺贝尔奖的日子恰好赶上两个值得记忆的日子。35年前的10月9日，安德烈・萨哈罗夫院士被授予诺贝尔和平奖。20年前的10月15日，苏联的首任和最后一任总统戈尔巴乔夫被授予同样奖项。<br />
<br />
　　如同当年的萨哈罗夫，今年又一位持不同政见者受到褒奖，他就是中国的刘晓波。刘难以领奖，因为他11年的徒刑刚刚开始，罪名是“煽动颠覆国家政权”。可以说，历史正在重演。萨哈罗夫当时也没有机会亲自到奥斯陆领奖。<br />
<br />
　　挪威诺贝尔委员会先是用15年走完了从褒奖苏联持不同政见者到褒奖苏共中央总书记这段路，然后游遍世界各地，又回到支持联合国安理会常任理事国中“争取基本人权的斗士”这个原点上，而这些国家并不总是赞同西方的民主概念。<br />
<br />
　　这种看上去迂回曲折的行为方式实际上反映了某种势力在遵循一个坚定的方针。大家可以自己作判断。从奖励萨哈罗夫到奖励戈尔巴乔夫表明，并不是挪威诺贝尔委员会成员为人处世的态度变了，而是国际政治发生了重大变化。苏联从冷战中的竞争者和敌人变成了西方建设性的和肯让步的伙伴，并且温顺地撤离了东欧。戈尔巴乔夫恰恰正是这种退缩的倡导者，柏林墙恰好倒塌，于是就找到了奖励对象。<br />
<br />
　　由挪威议会组建的诺贝尔委员会为什么要有5位智者呢？按照当初的设想，他们应该是脱离国际政治因素和只关心人类永恒价值的权威评议机构的成员。诺贝尔曾在自己的遗嘱中写到，和平奖应该授予为各国人民的团结、消灭奴隶制、减少军队数量和促进达成和平协议作出重大贡献的人。<br />
<br />
　　然而，这些抽象的范畴经受不住严峻现实的碰撞。诺贝尔和平奖一贯被政治化到极致。该奖主宰者的取舍和好恶取决于美国、北约和西欧的态度。这方面的例子很好找，看看刘晓波前面的3个获奖者就足够了。<br />
<br />
　　美国前副总统戈尔“因研究人类活动引起的全球气候变化造成的后果和制定可能加以防止的措施”而获奖。研究气候变化是重要和有益的事情，但这属于学术领域，它离争取和平要远一些。<br />
<br />
　　芬兰前总统阿赫蒂萨里则是按照北约和欧盟的模式强硬地解决前南斯拉夫问题的组织者。他积极支持轰炸塞尔维亚，是科索沃独立的促成者之一。显然，这个因素与其说导致了和解，不如说导致了巴尔干地区紧张局势的升级。<br />
<br />
　　再说说去年的获奖者美国总统奥巴马。授予他和平奖实在欠妥。当然可以怀疑基辛格、阿拉法特、拉宾等获奖者追求和平的诚意，但不管怎么说，他们都做了与和平有关的具体事情，如签署和平条约或解决冲突的计划等，而奥巴马在获奖时还什么都没干。实际上他是因为基于良好意愿的竞选承诺而预支了奖项。<br />
<br />
　　挪威诺贝尔委员会对美国这个世界大国采取了前所未有的做法，从而导致议论纷纷。人们认为该委员会没有独立性，其做法毁掉了诺贝尔遗嘱的精神。<br />
<br />
　　将2010年的和平奖授予同中国共产主义政权作斗争的刘晓波完全符合这种“欧洲大西洋战略”。在冷战正酣的时候，诺贝尔委员会大致上也是这样周期性地奖励苏联政权最不妥协和最凶恶的敌人。索尔仁尼琴在1970年获得诺贝尔文学奖。表面上他是因“遵循俄罗斯文学传统”而获奖，但这种说法令人无法不报以嘲讽。后来在1975年，授予萨哈罗夫和平奖时已不再掩饰政治动机了。<br />
<br />
　　很难说授予中国持不同政见者和平奖是否意味着西方与中国的冷战开始了。不过，毫无疑问，北京将会把这个决定视为不友好的纯政治举动。<br />
