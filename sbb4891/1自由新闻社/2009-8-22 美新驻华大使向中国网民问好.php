													<h2>美新驻华大使向中国网民问好</h2>
						
						
																											<div id="postmessage_211669" class="t_msgfont">于昨天（8月21日）刚到任的美国驻华大使洪博培大使（Jon Huntsman, Jr) ，克服时差，今天下午在他的北京官邸会见媒体，并向大家介绍他的幸福家庭。期间，他借我的推特（@mranti）信息，向中国推特用户和网友发出了第一声媒体祝福。<br />
<br />
我问：“我现在在Twitter上。中国有3亿网民，互联网在形成公民社会方面发挥极大作用。你能通过我的推，给中国网友说几句话吗？（I’m on Twitter now. We have 300 million netizens (Internet users) in China. Internet plays a very important role forming the civil society. Can you say something to Chinese netizens through my tweet?）”<br />
<br />
他的回答：“我想通过你的推特信息，告诉中国网友，对于中美两国来说，目前是极为重要的时期，我们要一起前进、分享利益。我们知道过去我们有过分歧，但今天的世界比历史任何时期都要严重依赖健康和安全的中美关系。这其中也包括通过现代手段如Twitter和其他类似载体来沟通的能力。现在很多人在写推特，很多人在写博客，这个国家有数千万博客。此种沟通代表了中美互相不断了解的能力，而这对于中美特别是对年轻一代来说，极为重要。”<br />
<br />
“I would say for your tweet that there is never going to be more important period for the United States and China, to go on and discover the shared interests. We know from time to time, we disagreed, but the world today, more than ever before, lies too much on a healthy and safe US-China relationship. And part of that, it’s the ability to communicate,&nbsp;&nbsp;through the modern means Twitter, and other similar vehicles.&nbsp;&nbsp;So those who were twittering out here, and those who were blogging, are many, tens of millions blogging in this country. This communication represents the ability to keep each other informed, and is critically important, particularly to younger generation.”<br />
<br />
使馆官员还私下告诉我，洪大使很快就要开始针对中国网友的自己的博客，要和中国网民做更好的沟通。希望未来的大使博客能在中国境内访问到。<br />
<br />
以下是洪博培大使在之前介绍自己和家庭时的开场白，大使交替使用中英文，这里全部翻译成中文，某些语气和助词有所更改。<br />
<br />
“北京的天气一直是这么漂亮，对吧？我特别高兴能在此代表美国和奥巴马总统，我今天尤其高兴在此向你们介绍我的家庭。我们昨晚刚到，今天开始正式工作，很多任务要完成。我们家庭乐于陪我到这里来，中国有句两千年之前的古话，“家庭快乐乃天伦之享”。今年中美关系有许多进展，30年中美复交纪念，中国建国60年，奥巴马总统也要在11月中旬访华，我在此告诉大家，到年底，中美关系将达到新高峰，期待更加正面的未来，面对各种潜在挑战，共享很多利益，11月奥巴马总统和中国领导人会见之后，我希望年底中美关系将比历史任何事情都强健。<br />
<br />
“我来介绍下我的家庭，Asha，她是我从印度领养的，她不会讲中文，但快要开始学了，今天中午她第一次吃了道道地地的川菜，她比较爱吃辣的，Gracie，这位就是Gracie，Grace Mei，来自扬州，中国有一句话说，扬州出美女，她就是扬州美女。她还没开始学中文，但后天就会开始学，我们之间有过承诺，我的老大叫Mary Anne，也非常漂亮，至今还没有一个男朋友。我现在有麻烦了。她很会弹钢琴，是钢琴家，等会我会带大家进去听她谈钢琴，她会在北京停留几个月，然后再返回美国。最后是我夫人，Mary Kaye，科罗拉多州人，我是加州人，我们生自美国不同地方，然后在中间相遇：我们已经很幸运地结婚25年了。<br />
<br />
“我们很高兴有机会一起到这里为美利坚共和国出使中国。我刚刚退出犹他州长的工作，那是一份很好让我很投入的工作。一直到奥巴马总统拨通我办公室电话说，有的时候，要超越政治之上，把个人野心放到一边，为更大的国家利益工作，比如中美关系我们都认为是世界上最重要的关系，因此今天我们才能来到这里。”<br />
