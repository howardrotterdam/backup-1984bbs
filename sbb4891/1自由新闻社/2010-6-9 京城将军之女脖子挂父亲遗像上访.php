作为中共统治中国以来，第一批被授予少将军衔的中共高级将领胥治中的83岁遗孀和他的56岁女儿胥晓琦，如今也陷入强拆前夕的恐慌和维权的磨难当中。<br />
<br />
胥治中去世后，遗留下一套连体别墅，共290多平米，因当年当局不让参加单位的房改，因此没有所有权，只有使用权，所以母女俩一直租住在此。2006年，第七研究院想拆除这套房子，改建为宿舍的后花园。胥晓琦告诉大纪元记者：“那时候就一直在逼我们以房改的名义离开，尽管没有产权，也有一个补偿安置问题 吧。现在他们不愿补偿安置我们，要把我们轰出去，让我们扫地出门。”<br />
<br />
面对没有任何补偿的情况下，父亲遗留下的住屋将遭到强拆，面临流落街头的胥晓琦和年迈的母亲，走上“上访”之途。胥晓琦告诉大纪元的记者：“我们上访的情况非常恶劣。国家信访局102室的一位中年妇女接待了我们。她说，我母亲不应该去上访，拒绝我母亲上访。她说，国家信访局不管，让找国务院事务机关管理局，说我们的房子归他们管。”<br />
<br />
不过，国务院事务机关管理局却对此表示，房子的产权不归属该单位，要求胥晓琦联系动迁的单位。胥晓琦说：“我让她给我们一个书面的答覆，她说这里不给书面答覆，拆迁的政策法规她不懂。”<br />
<br />
胥治中，1917年出生，不到13岁就当了兵。上个世纪60年代胥治中随供职单位转为地方干部，在中国船舶重工集团第七研究所担任过要职，也是该单位的创建者之一。1994年因病去逝。胥晓琦说：“他的一生都是为了中共建政，得过3枚勋章。一朝天子，一朝臣，现在的当权者，已经忘记了是谁为他们建立的政权了。”<br />
<br />
胥晓琦说：“老百姓特别同情我们，看着我爸爸的照片说：‘这样的人都受到这样的对待，我们都没有想到。他们只会喊口号，却不会给老百姓办实事。’”<br />
<br />
“将军夫人上访 哪个国家有这样的事啊？！”<br />
<br />
胥晓琦伤感地说：“他们连这个单位的创建者都这样对待，我认为这个国家这样下去的话，作为党的国家都没有什么希望了。我处在这样一个家庭背景，还有我自己的工作背景，我是不会轻易到外面去说自己的家丑的。现在连我都被逼着到外面去说自己的家丑，就是被逼上梁山了。”<br />
<br />
“不光是强拆，还侮辱人格。我不是一个轻易对社会失望的人，将军的夫人跟着我上访，哪个国家有这样的事啊？！我每个月都给很多部门和最高领导人写一封信，没有任何答覆。连我们这个情况都不管，更何况小小的老百姓。现在哪儿来的法制啊！如果没有法，这个国家怎么生存啊？！”<br />
