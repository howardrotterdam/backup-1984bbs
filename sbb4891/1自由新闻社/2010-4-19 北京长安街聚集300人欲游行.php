伍立娟（左）、斌（右）2008年曾因不Mxa而上L，分e遭囚禁一年。（明笥者z）<br />
<br />
【明报专讯】北京市中心的长安街爆发近年来最大规模民众聚集抗议事件。来自内地近20个省市2000多名4家国有银行的前职工，因不满早年有关银行改制裁员时对他们的赔偿不足，昨日上午到全国总工会、中国工商银行总部外请愿，其中有数百人被警员驱逐带走。据悉，仍有大批前「四行」职员正从各地赶赴北京，准备今日再赴银监会请愿。<br />
<br />
请愿者分别来自工商银行、建设银行、中国银行、农业银行。昨日上午8时，约2000人先后抵达位于东二环外南礼士路的全国总工会请愿，但总工会并无派人接待，请愿者在现场发起向青海玉树灾区捐款活动，每人捐款一元，并交给门口值班室。一小时后，请愿者再转抵位于长安街、距离天安门广场仅3公里的中国工商银行总行门前时，遭到严阵以待的公安阻止。<br />
<br />
300人被强行带走<br />
<br />
现场有人高喊：「胡主席！我们要吃饭」、「温总理！我们要工作」，人群更唱起《国际歌》，但一直未见银行人员出来接待。不久，公安开来7辆巴士先将人群围起，随后将他们押上巴士。至上午10时30分，其中约300人被强行带走。<br />
<br />
官方新华社昨日用200多字报道此事，称「警方已启动紧急预案，将大部分参与聚集人员送往国家信访局分流劝返中心。」据知，这些人被送至「劝返中心」后，将由各地驻京办、各银行等单位「认领」再遣返原籍。<br />
<br />
两前职员曾因上访被囚<br />
<br />
参与请愿的工商银行湖北支行职员伍立娟称，她在工行工作了18年，2002年湖北工行仅以2.1万元（人民币．下同）的补偿要她离职，「银行负责人曾威胁我，如3个月不签字就减70%，半年不签字就减至50%，9个月不签只有30%，如一年后还不签就一分钱不给。」她指自己至今没签字。而48岁的工行广西支行前职员陈斌，曾在工行工作近20年，2005年仅获工行以3.5万元补偿而离职，至今仍然失业。她们两人在2008年京奥前夕，到国家信访总局门前上访被拘，曾被囚禁一年。<br />
