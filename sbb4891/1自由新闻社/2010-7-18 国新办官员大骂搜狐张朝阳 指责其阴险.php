&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;这位网管局的领导说，前段时间修理搜狐网是真的，搜狐自从张朝阳年初讲话后，下面得寸进尺，容忍很多不健康内容，一下子成了各大网站中最冒头的。但他也强调，只是以搜狐为起点，还会对网易与腾讯做一些清理整顿工作。 (博讯 boxun.com)<br />
<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;他随即就开始大骂搜狐的老板张朝阳，他说，网管局确实一度要求搜狐微博关闭整顿，也指出他们的博客有些问题，但我们最关注的是他们的评论与新闻跟帖。可是，张朝阳就阳奉阴违，在我们已经停止了整顿，也重开了搜狐的微博的时候，张朝阳突然下令一下子关闭了一百多位搜狐博客。这些博客都是很有影响力的博客。有些内容确实有问题，可大多数并不应该一下子关闭。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 张朝阳如此作为，给外界一个印象，好像是北新办逼迫他关闭博客，他是万不得已的受害者，其实，这些博客都是搜狐自己一夜之间挑选出来关闭的，而挑选的这些博主都是很有影响力，又能搞事的，有些还是国内外著名的人物，例如贺卫方、杨恒均、章力凡、许志勇、滕彪等。搜狐之所以挑选这些人的博客关闭，而且事前一个招呼也不大，根本目的就是想借被关掉博客的这些名人来炒作一把，一是扩大搜狐在国内外的影响力（他们认为这次整顿他们是吃了哑巴亏），二是让国新办网络局难堪。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 这位国新办领导说，搜狐张朝阳肯定是达到了目的，这几天，海外多家媒体和外国驻华机构打电话到国务院新闻办与国新办，询问博客被关的事。弄得政府很被动，回答他们说这些博客关闭不是政府的指令吧，也不对，毕竟政府是要求各大网站整改并关闭博客的；说是政府的指令吧，我们这次却是没有让他们关闭某个具体人的博客，更不会用这种一下子关闭一百多个名博的蠢办法。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 这位国新办领导还说，张朝阳这人很阴毒，他看到这几年形势有些变化，很有点想从政的打算，搜狐就快要成为他的从政工具了。这次这种做法就很明显。不过，他要给外人一个北京政府在压他关闭博客的印象，也有事与愿违的，因为大家可以问一下其他门户网站，是否收到了类似的指令？我们不会只要求搜狐收紧，对别的网站网开一面吧？(boxun.com)<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 在一位在座的陪同外事办领导一起来吃饭的年轻秘书询问是否有管部门要对张朝阳年初的一个讲话进行报复的时候，这位领导说，至少从他了解到的情况看，没有这回事。他说，一个吃喝嫖赌的人，偶尔发了一次那种言论，有什么大不了的？外界过分解读了。<br />
&nbsp; &nbsp; <br />
