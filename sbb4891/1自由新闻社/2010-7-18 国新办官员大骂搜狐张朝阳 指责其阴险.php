这位国新办领导还说，张朝阳这人很阴毒，他看到这几年形势有些变化，很有点想从政的打算，搜狐就快要成为他的从政工具了。这次这种做法就很明显。不过，他要给外人一个北京政府在压他关闭博客的印象，也有事与愿违的，因为大家可以问一下其他门户网站，是否收到了类似的指令？我们不会只要求搜狐收紧，对别的网站网开一面吧？<br />
