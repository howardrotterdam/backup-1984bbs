他随即就开始大骂搜狐的老板张朝阳，他说，网管局确实一度要求搜狐微博关闭整顿，也指出他们的博客有些问题，但我们最关注的是他们的评论与新闻跟帖。可是，张朝阳就阳奉阴违，在我们已经停止了整顿，也重开了搜狐的微博的时候，张朝阳突然下令一下子关闭了一百多位搜狐博客。这些博客都是很有影响力的博客。有些内容确实有问题，可大多数并不应该一下子关闭。<br />
张朝阳如此作为，给外界一个印象，好像是北新办逼迫他关闭博客，他是万不得已的受害者，其实，这些博客都是搜狐自己一夜之间挑选出来关闭的，而挑选的这些博主都是很有影响力，又能搞事的，有些还是国内外著名的人物，例如贺卫方、杨恒均、章力凡、许志勇、滕彪等。搜狐之所以挑选这些人的博客关闭，而且事前一个招呼也不大，根本目的就是想借被关掉博客的这些名人来炒作一把，一是扩大搜狐在国内外的影响力（他们认为这次整顿他们是吃了哑巴亏），二是让国新办网络局难堪。<br />
