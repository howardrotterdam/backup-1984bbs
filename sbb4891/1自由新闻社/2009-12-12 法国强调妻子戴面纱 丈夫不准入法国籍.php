													<h2>当欧洲遇到戴面纱的穆斯林（2007年的文章）</h2>
						
						
																											<div id="postmessage_369635" class="t_msgfont">《财经文摘》<br />
很少有争议能够像穆斯林面纱问题那样令欧洲人产生如此大的分歧。<br />
穆斯林的众多面孔：不管是新近的移民，还是第三代穆斯林妇女，选择的头部装饰范围很广，从头巾、面纱到没有任何遮盖物。 <br />
在过去的一周里，荷兰政府表示打算禁止穆斯林妇女在公共场合穿戴面纱；梵蒂冈教廷宣布佩戴面纱是对当地文化和当地人感情的一种不敬；德国北威州政府官员表示如果穆斯林教师违反不允许戴头巾的规定，政府将作出处罚；英国前外交大臣斯特劳对于这一跨文化的敏感话题更是火上浇油，他说：“此类的穆斯林装束‘隔离了人们’，阻碍了融合。因为交流需要双方看到对方的面部，你不但需要听到，也需要看到人们在讲什么。” 英国首相布莱尔也将头巾视为“隔离的标志”。<br />
<br />
难以融合的少数人群 <br />
谁会想到面积不足20平方厘米的小布会引起如此的轩然大波呢？因为只有很小一部分西欧的穆斯林妇女习惯佩戴面纱。这不是穆斯林妇女的装束第一次导致危机，也不会是最后一次。面纱成为了不和谐的因素，增加了西方国家人群对穆斯林的怀疑：穆斯林人是难以融合的少数人群，与欧洲的生活方式不一致，并带来了安全威胁。<br />
面纱危机的首要原因是宗教，本文作者阿拉姆就是一个好例子。作为一名年轻、受过良好教育的英国职业女性，她因为自己的信仰而选择佩戴头巾。她虽然出生在伦敦，但是在新加坡长大，以前家里并没有人佩戴头巾。她在大学时“发现了”伊斯兰教，并选择了佩戴头巾，最初的原因更多的是为了表明自己的身份，以及对所信奉的价值观表现出来的自信。随着时间的流逝，佩戴头巾成为了一种她对伊斯兰教表达虔诚的方式。在英国布莱克本市穆斯林聚居区，越来越多的年轻、受过教育的女性选择佩戴面纱，比较典型的是一所大学的心理学教师梅亚特，她相信面纱是与伊斯兰教教义最接近的宗教装束。<br />
<br />
其次，英国国内对面纱问题的争论同发生在法国的头巾问题类似，更多的是与欧洲人对自身身份确认的危机有关。在后共产主义时代，原来的欧洲人普遍认为他们应该在欧洲的历史中发挥决定性的作用。但是，生机勃勃、具有宗教色彩的穆斯林团体却正在改变着欧洲的格局。统计表明，移民以及高生育率因素使穆斯林人正在改变着欧洲的人口构成。以奥地利为例，预计穆斯林人占该国的人口比重将会从现在的4%上升到2050年的26%。像作者这样生活在伦敦，受宗教和文化传统影响的年轻穆斯林们普遍对伊拉克的崩溃和所谓的反恐战争表示愤怒，但他们同时又认为自己是英国国民，有权以任何方式面对世人。<br />
<br />
而另一方面，有批评人士认为欧洲的穆斯林人将过多的精力放在了捍卫他们信仰的外在标志上。英国纪录片导演纳维德・阿客达认为：“这种由面纱引发的危机暴露了人们精神上存在的真空，我们忘记了我们信仰的是上帝，而不是我们的头巾或我们的胡子。如果你的邻居对你感到害怕，并不都是他的错，你也有责任做出让步，并让你的邻居冷静下来。”<br />
<br />
那些感受到由面纱引发的危机的人可能会问另外一个问题：要求欧洲的穆斯林人融入欧洲社会的呼声到底指的是什么？有很多比面纱更重要的因素造成了穆斯林人和其他欧洲人之间的隔离：贫穷、住房、缺少工作和教育机会。最后需要明确的一点是，欧洲的穆斯林人不再是外国人了，他们生活在欧洲，并且大多数人已经融入到欧洲社会，这与她们戴不戴面纱无关。<br />
