													<h2>人大官员：政府开支不高 公务员工资偏低</h2>
						
						
																											<div id="postmessage_687674" class="t_msgfont"><img src="http://www.vanke.com/files/03F03C45-30DA-453C-8B96-681AEB9A455C/gaoqiang.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
原卫生部长、人大财经委副主任委员高强日前接受媒体访问时发表自己对政府预算公开相关问题的看法。日前各级政府公开的预算令民众感到政府支出过高，对此高 强回应与国外很多国家相比中国的政府支出并不太高，并称在预算报告中讲清楚&quot;三公&quot;支出仍存在困难。<br />
<br />
新京报6月17日报道 高强，全国人大财政经济委员会副主任委员、全国人大常委会预算工作委员会主任。<br />
<br />
 <br />
<br />
财政工作是高强的老本行，他仕途中有30多年是在财政系统度过的，曾担任过财政部预算司司长、副部长。后离开财政八年，先后任国务院副秘书长，卫生 部党组书记、卫生部部长，2009年又“重操旧业”。“在人大从事预算工作与财政部有很大不同，不是具体的资金分配和管理，而是站在宏观、全局和法制的角 度，通过审查和监督，推动政府管好钱、用好钱。”他说。<br />
<br />
 <br />
<br />
2010年3月全国人代会期间，高强曾经透露，今年将公开中央部门预算，告诉公众政府怎么花钱。<br />
<br />
 <br />
<br />
高强在接受本报记者采访最后，总结其在政界几十年的准则是：“坦诚、真实”，面对记者提出的预算公开、预算编制、“三公”支出、预算法制等敏感问 题，高强有问必答，丝毫也不回避。<br />
<br />
 <br />
<br />
高强简历<br />
<br />
 <br />
<br />
汉族，河北盐山人，1944年8月生，毕业于中国人民大学经济系世界经济专业，1967年参加工作，1978年7月加入中国共产党，高级经济师。<br />
<br />
历任河北省财政厅副处长、副厅长，财政部财政预算管理司副司长、司长、部长助理、副部长等职。<br />
<br />
 <br />
<br />
2001年7月任国务院副秘书长兼国家科教领导小组办公室主任，2003年4月任卫生部党组书记、常务副部长，2005年4月至2007年6月任卫 生部党组书记、部长。2007年6月任卫生部党组书记、副部长。<br />
<br />
2009年2月被任命为第十一届全国人大财政经济委员会副主任委员、全国人大常委会预算工作委员会主任。<br />
<br />
第十七届中共中央委员。<br />
<br />
 <br />
<br />
公开“三公支出”需要做大量基础性工作<br />
<br />
 <br />
<br />
全国各级政府的“三公”支出不能都由财政部编制出来，即使编制出来也不一定准确。<br />
<br />
 <br />
<br />
新京报：今年全国人大在推进预算公开方面加大了工作力度，这是对公众关切的回应吗?<br />
<br />
 <br />
<br />
高强：政府向社会民众公开预算，是民主法制建设的必然要求。财政预算是取之于民、用之于民，民众有权了解政府的预算安排并进行监督，这是公共财政的 应有之义。<br />
<br />
很多年前，中国的知识界就提出过实行国家预算公开的 “梦想”，现在正在做的，是把这个“梦想”变为现实。<br />
<br />
随着我国社会主义政治建设的不断发展，民主决策、政务公开、民主监督等制度都在日趋完善，预算公开不仅是社会民众的强烈愿望，也已经成为政府接受群 众监督的一个重要渠道。<br />
<br />
民众应该知道各级政府一年收入多少钱，来自哪些方面;一年支出多少钱，用在哪些方面。不仅了解收支总账，还要了解具体开支项目，让群众能够客观评价 政府财政支出是否符合党和国家的方针政策、是否符合广大人民群众的意愿、是否用于经济社会发展和改善民生最急需的地方。国务院颁布的政府信息公开条例明确 规定，国家预算、决算报告属于必须公开的范围。<br />
<br />
因此，我们现在谈论的不是政府预算公开不公开的问题，而是如何主动公开、及时公开和有效公开的问题。<br />
<br />
可以说，现在推动政府预算公开，各方面条件已经成熟。<br />
<br />
 <br />
<br />
新京报：全国人大在推动预算公开问题上，存在什么困难?<br />
<br />
 <br />
<br />
高强：对于预算公开，一是法律有规定，二是群众有要求，三是政府有积极性。人大的工作，主要是监督和推动，促进政府改进预算公开工作。<br />
<br />
预算公开面临的最大困难，是预算管理的很多基础性工作还不够完善。<br />
<br />
 <br />
<br />
新京报：本届人大任期到2013年，到时候预算公开会达到一个什么程度?<br />
<br />
 <br />
<br />
高强：推动预算公开不是目的，最根本的是通过预算公开加强人大对政府预算管理的法律监督和社会的民主监督，促进政府改善预算管理，用好人民赋予的财 权，实现预算编制的完整性、预算科目的规范性、预算执行的严肃性和预算监督的有效性。<br />
<br />
要做好这些工作，我们不仅要加强与政府部门的沟通，还要加强与社会民众的联系，了解大家的愿望和要求，发挥民主监督的作用。这样，预算公开才是有效 的。在这个问题上，人大、政府和社会民众的目标是一致的，而不是对立的。<br />
<br />
 <br />
<br />
新京报：加强沟通的确非常重要。现在民众还有更高的要求，大家并不是只想看看预算，而是要监督。但从目前公开的部门预算看，想要监督很难。<br />
<br />
 <br />
<br />
高强：公开是形式，监督是目的。<br />
<br />
在预算公开问题上，政府部门首先想的不是我要公开什么，而是社会民众需要我公开什么。如果由于基础工作条件不具备而一时做不到，就要向民众解释清 楚，并积极创造条件努力做到。<br />
<br />
现在大家都关注“三公”支出问题，要求在公开预算中将“三公”支出情况讲清楚。从当前预算管理的基础工作来看，还存在一定困难。因为我国财政实行 “分级管理”制度，国家预算分成中央预算和地方各级政府预算，全国各级政府的 “三公”支出不能都由财政部编制出来，即使编制出来也不一定准确。只能按照“分级管理”的原则，由各级政府去自行编制、自行公开，对人民负责。<br />
<br />
对于政府各部门，目前实行综合预算管理，情况非常复杂。在部门预算中，既包括部门机关本身的支出，还包括所属事业单位的支出;既包括财政拨款支出， 还包括所属单位自己组织事业性收入安排的支出，政府各部门也不可能将所属单位一年的各项支出都编制出来。<br />
<br />
比如，教育部的部门预算，既包括教育部机关运行所需的支出，还包括所属100多所大学的收入和支出。<br />
<br />
卫生部的部门预算既包括机关的运行支出，也包括所属的40多家大型医院的收入和支出。在这些所属单位的预算中，既包括财政拨款的支出，又包括单位组 织收入安排的支出。这些单位的支出，有一部分是用财政拨款安排的，还有一部分是用单位的事业性收入安排的。虽然这些支出列入了部门预算，但又不是真正意义 上的“财政支出”。因此，大家反映对部门预算看不懂。我认为，这不是预算公开的问题，而是预算管理制度和基础性工作不完善的问题。<br />
<br />
今年有几十个中央国家机关公布了部门预算，这是个很大的进步。但在完善预算编制和预算管理制度方面，还有很多工作需要改进。<br />
<br />
 <br />
<br />
按季度向全国人大代表通报预算执行情况<br />
<br />
 <br />
<br />
让人大代表看懂预算报告并充分发表审议意见，是一种责任。<br />
<br />
 <br />
<br />
新京报：有些人大代表反映，开人代会时审议预算的时间太短，预算的专业性太强，给代表审查预算造成很大困难。<br />
<br />
 <br />
<br />
高强：这种情况确实存在。前几年，大家反映预算编制太粗，要求细化预算。各级政府向人大代表陆续增加了大量有关预算的资料。由于人代会召开的时间不 长，又有多项议程需要审议，审查预算的时间相对来说比较短。<br />
<br />
为了解决这个问题，就需要充分发挥人大财经委和人大常委会预算工委的作用。<br />
<br />
从今年开始，我们将进一步改进为人大代表服务的工作，准备按季度向全国人大代表印送预算执行情况的简报，使人大代表能够及时了解和掌握 国家预算的执行情况。在人大财经委对下年预算进行初步审查后，也会将初步审查意见提前印送全国人大代表参阅，使人大代表能够及时了解预算执行情况和预算安 排的有关信息，以便提前准备好审查意见，到人代会上充分发表。<br />
<br />
 <br />
<br />
新京报：对人大代表反映看不懂预算报告的问题，有一种流行的解释：预算很专业，大家都看懂是不可能的。你怎么看?<br />
<br />
 <br />
<br />
高强：这种解释是不全面的。<br />
<br />
从预算管理来讲，确实专业性比较强。我国现在分为中央、省、市、县、乡等五级财政，各级预算既涉及预算编制、预算执行、预算调整、决算编报以及预算 执行监督等项工作，还涉及各级政府之间的财政关系，包括支出责任划分、收入划分、税收返还、转移支付等项业务，确实比较复杂。<br />
<br />
但从预算报告来说，面对的是广大人大代表和社会民众，就不应该强调专业性，而应该做到通俗易懂，将政府一年要收入多少钱，这些钱从哪里来;一年要支 出多少钱，用到哪些方面，取得什么效果，向人大代表和社会民众讲清楚。<br />
<br />
让人看不懂的预算报告不是一个好报告，让人看不懂的预算也不是一个好预算。让人大代表看懂预算报告并充分发表审议意见，是一种责任。在编制年度预算 时，可能对各部门、各单位的支出还难以安排得很具体，但在编制决算时，就应该客观、真实地反映各项支出的具体情况。<br />
<br />
 <br />
<br />
政府决算也应逐步公开<br />
<br />
现在预算已经公开了，今后还要创造条件，逐步公开政府决算，包括部门决算。<br />
<br />
 <br />
<br />
新京报：就是说公开部门决算更有意义?<br />
<br />
 <br />
<br />
高强：政府信息公开条例要求公布政府的预算报告和决算报告，现在预算已经公开了，今后还要创造条件，逐步公开政府决算，包括部门决算。<br />
<br />
 <br />
<br />
新京报：不少人把预算编制、预算执行和预算公开的过程，与扩大人民民主联系起来，你多年从事财政工作，对这个问题怎么理解?<br />
<br />
 <br />
<br />
高强：预算编制的过程应该是一个民主决策的过程。财政部门在编制预算时，要先征求各个部门对下年预算安排的意见，了解各部门的实际需求;经过综合平 衡后提出初步安排意见，反馈给各部门;各部门再提出意见，财政部门又进行适当调整。最后由财政部门提出预算编制方案，经过国务院审议，形成预算草案，报人 代会审查批准后，再由财政部门正式下达各部门执行。<br />
<br />
今后在预算编制过程中，除了认真听取各部门的意见外，还应该关注民意，了解民众的需求和愿望，使预算编制的过程成为集中民智、反映民意的过程。<br />
<br />
 <br />
<br />
明年预算报告将反映政府行政经费支出情况<br />
<br />
现在的预算科目中，不能反映政府行政费用的开支情况，大家很有意见。<br />
<br />
 <br />
<br />
新京报：公众还是很关心政府行政支出。<br />
<br />
 <br />
<br />
高强：公众对这方面的开支很关心可以理解。<br />
<br />
财政预算是国家经济社会发展和改善民生的重要保障，政府行政经费支出只是一小部分。如果用于政府行政费用太多，就会影响社会事业的发展。现在的预算 科目中，不能反映政府行政费用的开支情况，大家很有意见。<br />
<br />
为此，今年全国人大财经委的预算审查结果报告中提出“细化政府预算科目，提高预算透明度。 2011年，向全国人大报告基本建设、行政经费等社会关注的项目支出情况”，回答大家所关心的问题。<br />
<br />
 <br />
<br />
新京报：人大代表如何审议这些支出呢?<br />
<br />
 <br />
<br />
高强：主要审查行政开支占财政总支出的比重和具体安排是否合理。如果政府行政开支占总预算的比重太大，说明行政支出太高了，也可能存在一些铺张浪费 的问题;如果行政费用所占比例太低，也会影响政府正常的运转，政府履行职责也需要有基本的财力保障。<br />
<br />
 <br />
<br />
新京报：公众的感觉是政府开支太大了。<br />
<br />
 <br />
<br />
高强：中国政府本身的开支水平与全世界很多国家比较，并不太高。一是我国行政人员占总人口的比例比其他国家要低，二是公务员的工资水平与很多国家相 比也比较低。<br />
<br />
为什么大家感觉政府开支很高呢?因为现在不少行政机关既有机关运转所需的行政支出，也管理着一些事业发展的项目支出，执行中有些部门存在行政性开支 挤占事业费的情况。因此，必须把行政机关支出与事业发展支出严格区别开来，严禁挤占挪用。<br />
<br />
同时，我们也应该看到，现在行政支出中确实存在不少损失浪费问题，公款出国、公款用车、公款接待数额比较大，既有合理的，也有不合理的，甚至存在一 些腐败现象。这也是民众关心“三公”支出的根本原因。要解决这个问题，不仅要公开预算，更重要的是改革公务用车制度，严格控制公款出国和公款接待，进一步 严肃财经纪律。<br />
<br />
 <br />
<br />
完善预算科目设置满足民众需求<br />
<br />
实现这个目标，不仅财政部门要做，其他部门也要积极配合，逐步推进完善。<br />
<br />
 <br />
<br />
新京报：大家不仅想看到政府支出的方向，也想看到政府支出的具体用途，这个问题怎么解决?<br />
<br />
 <br />
<br />
高强：解决这个问题，应逐步完善预算科目体系。既要按照支出功能，设置预算科目，客观反映教育、文化、卫生、科技、国防、社会保障等方面的支出，反 映财政支出的方向;也要按照经济分类，客观反映预算用于人员工资、设备购置、办公经费、事业发展、基本建设等方面的支出。两套预算科目体系相辅相成，就可 以比较清楚地反映预算支出的具体情况。<br />
<br />
实现这个目标有很多基础性工作要做，不仅财政部门要做，其他政府部门也要积极配合，逐步推进，逐步完善。<br />
<br />
修预算法时应明确，支出超过预算要报人大常委会批准<br />
<br />
如果公开的预算是不完整的或没有严格执行，又缺乏严格的监督机制，预算公开就失去了意义。<br />
<br />
 <br />
<br />
新京报：预算法修订草案8月份要提请人大常委会审议，财政和审计学者们呼吁，希望公众在关注预算公开的同时，也关注预算的完整性和预算执行的规范性 问题。<br />
<br />
 <br />
<br />
高强：预算编制的完整性、预算执行的规范性和预算监督的严肃性，是国家预算管理最基本的内涵，也是预算公开的基础。如果公开的预算是不完整的，或者 公开的预算没有严格执行，又缺乏严格的监督机制，预算公开就失去了意义。<br />
<br />
在预算法的修订中，维护预算的完整性、预算执行的规范性和预算监督的严肃性，与预算公开一样，都是必须强调的基本原则。<br />
<br />
 <br />
<br />
新京报：我在采访中也听说，目前各级预算在执行中随意调整的现象相当普遍。在预算法修改后，预算调整能否受到法律约束?<br />
<br />
 <br />
<br />
高强：各级预算经同级人大批准后具有一定的法律效力。现行预算法规定：“经本级人民代表大会批准的预算，非经法定程序，不得改变。”但在实际工作 中，对这些规定执行得不够严格，有些政府预算未经法定程序也作调整了。<br />
<br />
在修订预算法时，应该对这个问题做出更加明确的规定，凡是预算支出总额超过人大批准的数额，都必须制订预算调整方案，按照宪法规定报经人大常委会批 准。这是规范预算执行的一项重要的保障。<br />
<br />
 <br />
<br />
新京报：如果预算执行中有一些具体支出科目的变化，算不算预算调整?<br />
<br />
 <br />
<br />
高强：按监督法的规定，对于国家规定的一些重点支出项目，如教育、科技、文化、卫生、社会保障等，如果在执行中因情况变化，实际支出比预算减少，也 要作为预算调整，提出预算调整方案，报经人大常委会批准。<br />
<br />
 <br />
<br />
高强讲解预算报告<br />
<br />
对普通公众而言，预算工作显得有些陌生。政府部门的预算报告是怎么出来的?预算报告内容如何去理解分析?高强在接受专访时，对这些问题也一一作了指 点，进行了一番预算知识的“普及”。<br />
<br />
 <br />
<br />
预算编制有几种方式?<br />
<br />
一种是基数法，将上一年各项支出的预算执行数作为基数，安排下一年预算时，反映比上年执行数增加了多少，增长了多大比例。这种办法编制预算，比较简 单，也可以使人一目了然。基数法也存在一些缺陷，就是比较粗，缺少对一些具体项目的安排，如果上一年支出中的有些项目在下一年发生了变化，也都作为基数包 含在下年预算之中，就不够合理了。<br />
<br />
另一种是因素法，也称为“零基”预算法，就是不将上一年预算执行数作为基数，而是根据预测下一年的各种支出因素重新测算。这种办法比较科学，但要做 很多基础性工作，预算编制的工作量非常大，而且也不易预测得很准确。<br />
<br />
目前我国编制预算，主要采取基数法。按因素法编制预算，条件还不具备。<br />
<br />
预算编制有哪些程序?<br />
<br />
财政部门编制预算，要根据国家确定的经济社会发展总目标和总要求以及各项方针、政策，先征求各部门的意见，了解各部门的支出需求，经过综合平衡后提 出对各部门下年预算安排的初步意见，再次征求各部门意见，财政部门再对预算安排作出适当调整，这个程序俗称“二上二下”。最后由财政部门提出预算编制方 案，经政府审议后，形成预算草案，报同级人大审查批准，再由财政部门正式批复各部门执行。<br />
<br />
审查预算主要看什么?<br />
<br />
一是看收入预算安排，包括上一年各项税收和非税收入完成情况，下一年各项税收和非税收安排情况。主要看各项收入增长幅度与国家经济增长 幅度是否适应，与国家收入政策是否吻合。如果收入增长远远高于经济增长，说明收入安排太多了，可能会影响经济的发展;如果收入增长幅度太低，也不适应各项 预算支出增长的需求。<br />
<br />
二是看支出预算安排，主要看“三农”、教育、卫生、文化、科技、社会保障、环保、国防等项重点支出的增长，是否符合国家的方针政策，是否反映人民的 期望，是否用于国家经济社会发展最急需的方面。<br />
<br />
至于一些具体的项目支出情况，主要应在部门预算中反映。</div>

							
							
							
							
