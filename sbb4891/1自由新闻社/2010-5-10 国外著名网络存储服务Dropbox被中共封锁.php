&nbsp; &nbsp; 差不多整一个月前，我通过williamlong知道了dropbox，在简单的了解之后，便不可救药的喜欢上了这个软件，它简直是为我等量心打造的，太体贴了，以致我在第二天清晨起来，忍不住写了一篇推介它的文章《玩转网盘》（现更名为玩转Dropbox），然后又极力推荐给几位朋友，其中一位今天下午还和我交流使用心得呢，我感觉特对不起那朋友，因为这就好像你给朋友介绍了一位绝世美女，很快他们情投意合，难解难分，但过了两天你突然跟朋友说：哥们，不好意思，那美女失踪了。<br />
&nbsp; &nbsp; 本以为，在经历了世界上那么多第一流网站被封杀之后，我应该淡然了。YouTube没了，Facebook没了，Twitter没了……，曾经愤怒过，义愤填膺那种，一次次下来，觉得自己已经被锻炼的麻木了，但是没有，每一次的血腥，都生理性的激起我的愤怒。然而，愤怒又能怎样，在以前，嘴上骂一通，再心里腹诽一番，愤怒也就成往事，渐渐成为自己回忆的谈资而已；在现在，愤怒不过是换一种方式消解而已，可以写在这里通过这一个个方块字消解。愤怒不能阻挡下一个世界一流网站的死亡，只能继续眼看着那个世界一流杀手的狞笑。<br />
&nbsp; &nbsp; 去年，当Twitter被封，我就说：“中国，自绝于时代”，现在这种自绝仍在继续，当中国网民只能使用那些傻逼网站还自以为乐时，我们就可以骄傲的称我们终于又回到文明古国了。<br />
&nbsp; &nbsp; 上次我写“玩转”dropbox，这么快就得写悼词“玩完”，真是哭笑不得了。<br />
