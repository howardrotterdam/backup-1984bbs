<br />
我把月光博客的这篇《Dropbox无法访问》的贴在放在这里，您可以了借此解更多：<br />
<br />
&nbsp; &nbsp; 今天下午，世界上最伟大的云存储服务之一――Dropbox被证实无法从中国地区访问，客户端和网站均无法正常连接和登录，我先前曾经多次推荐过这个伟大的服务，没想到这么快Dropbox就惨遭毒手。<br />
　　目前，中国用户已经无法通过Dropbox客户端和Web网页访问Dropbox，已经连接上Dropbox服务器的客户端一经退出就会无法再登录，显示的现象是Connecting一直在持续却连接不上，从客户端点“Launch Dropbox Website”也无法打开网页登录。<br />
　　封锁Dropbox的方式是关键字封锁和IP封锁，关键字为“.dropbox.com”，IP为Dropbox官方网站的IP，可谓是全方面地定点封锁。<br />
 <br />
　　当确认证实这个消息的时候，我的心情是复杂和震惊的，进行这个操作的人啊，你是否知道你在做什么？你让全中国的网民无法使用这个世界上最优秀的云存储服务，这种没有任何意识形态的专业类服务网站为什么也会遭此毒手呢？使用这个服务的大多是拥有多台电脑的资深网民，现在都必须面对这个现实：我们生活在一个与互联网为敌的国家，一切优秀的互联网服务在这个国家都无法正常运行，这个国家已经病入膏肓、无可救药了。<br />
　　Dropbox是这几年来唯一的一个我手动添加到“启动”组里的客户端软件，是一个优秀的在线存储服务，通过云计算实现网际网络上的文件同步，用户可以存储并共享文件和文件夹。这个小软件让我能轻松的管理我三台电脑的文件、图片、资料文档，几台电脑之间的文件同步和更新均自动完成，每天电脑都自动下载最新版本的文档，在任何一台电脑更新文档之后，都会自动上传到云存储服务器端，接着同步到其他电脑，如果没有这个服务，多台电脑之间的文件同步和更新将让我头痛，我可能将拿着U盘在几台电脑间忙于复制粘贴，毫无意义地浪费自己的宝贵时间。<br />
