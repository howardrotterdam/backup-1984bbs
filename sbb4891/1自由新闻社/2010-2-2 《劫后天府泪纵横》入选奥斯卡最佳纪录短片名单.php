<br />
当地时间1月24日，美国制片人工会（the Producers Guild of America）揭晓了今年的获奖名单，年度大热《拆弹部队》勇夺最佳影片奖。<br />
<br />
就在前不久，《拆弹部队》刚刚获得美国评论家选择奖最佳影片，此外它还获得了国家影评人协会奖和金卫星奖、以及多个地区影评人协会大奖，并入围了编剧、导演、剪辑等多个工会的提名，在本年颁奖季上大放异彩，风头甚至盖过了刚刚获得金球奖的《阿凡达》。<br />
