　　金球奖最佳剧情类影片《阿凡达》(Avatar)和广播影评人协会奖最佳影片《拆弹部队》(The Hurt Locker)并列九项提名，成为现阶段的领跑者。问鼎演员工会奖最佳群像演技的《无良杂牌军》(Inglourious Basterds)以8项提名数紧随其后。备受评论界赏识的《在云端》(Up in The Air)以6项提名数成为入围名单上的第四大户，不过失意于号称“小最佳影片奖”的最佳剪辑的提名对本片来说绝非好消息。<br />
