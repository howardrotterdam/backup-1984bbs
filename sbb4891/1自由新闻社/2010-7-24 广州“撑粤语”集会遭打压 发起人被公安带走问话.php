港家长逼子女讲英语<br />
<br />
另外，英文《国际先驱论坛报》前日评论版就登篇香港自由撰稿人 Verna Yu文章，题为《 Cantonese, Please》（讲广东话吖，唔该），有个几有趣观察。作者话，原本用母语同自己小朋友沟通系世间最自然事，但障愀圻硐蹈龈钰牛早前谖两岁囡囡搞生日派对，谟霉愣话问个 4岁来宾想食乜，对方净系用英文大叫&quot;No,no,no!&quot;後来先知，香港唔少家长就算本身英文麻麻，都坚持同自己个 BB全英文对话，为就系想仔女学好英文，考入名校或者国际学校。学好英文梗系好，国际化梗系正，但本土文化根，又系咪好事呢？<br />
(译:另外，英文《国际先驱论坛报》前日评论版就刊登了篇香港自由撰稿人 Verna Yu的文章，题为《 Cantonese, Please》（讲广东话吧，谢谢），有一个蛮有趣的观察。作者说，本来用母语和自己的小朋友沟通是世间最自然的事，但在香港并不是每个人都这样想，不久前她为两岁的囡囡搞生日派对，她用广东话问一个 4岁的来宾想吃甚麽，对方只用英文大叫&quot;No,no,no!&quot; 後来才知道，香港不少家长就算自己的英文很一般，都坚持和自己的 BB全英文对话，为的就是想子女学好英文，考入名校或者国际学校。学好英文当然是好，国际化当然很利害，但没有了本土文化的根，又是不是好事呢？)<br />
