																											<div id="postmessage_756659" class="t_msgfont">广州「撑粤语」集会遭打压 发起人被公安带走问话<br />
O果日, 2010年07月24日 <br />
<br />
广州 80後原定在明日（ 25日）傍晚於海珠区江南西地铁站外举行「齐撑广州，齐撑粤语」集会，但昨日传出遭当局打压，不但取消原来的备案，将其定性为「非法集会」，同时更将活动的发起人带走问话。<br />
<br />
但民众未有因此放弃，部份人在网上串连，计划当日在全市各地举行各式撑广州话活动，务求遍地开花。<br />
<br />
有关齐撑广州话活动遭打压的消息昨在内地多个论坛及微博上传出。据称原计划在明日下午 5时半在江南西地铁站 A出口外举行的集会，不获当局批准，更被定性为非法集会。<br />
<br />
按原来规划，这次活动会在地铁站出口举行派物活动，工作人员会向参加者问广州话歇後语问题，若果答中，可以获赠一件印有明代名将袁崇焕名句「掉哪妈！顶硬上！」的 T恤，同时大会亦计划会大呼口号，暂定的口号包括「我爱广州！我讲广州话！」等。主办者预计会有两万民众参加，并且呼吁民众穿白色上衣出席。<br />
<br />
消息传出後，网民反应两极，部份网民认为这是一早预料得到的结局，当局不可能批准这种集会，可是也有网民质疑讯息是由当局网上写手丶俗称「五毛党」发出，强调相关讯息仍未被确定，质疑「当日着白色衫在地铁站外就会被拉」？继续呼吁民众当日准时出席。<br />
<br />
民众：要撑就撑到底<br />
<br />
「要撑就撑到底！」不少网民称，明日会在不同时间到地铁站外「刺探军情」，并在微博上公布。由於活动在傍晚才开始，很多广州民众表示，日间会安排各式各样支持广州话的活动。有人准备在越秀区中山四路举行「唱好粤语」的聚会，细数历年来的粤语流行曲，也有人串连在快餐店内举行「吹水会」等，然後一同坐地铁到江南西参加撑广州话集会。<br />
