<br />
广州爆发「粤战」，难得当地年轻人罟匕自己本土语言同文化，八方作为一直身体力行以广府话写作一员，梗系力撑。但x日传来内地当局打压原定听日展阒菥傩「齐撑粤语大行动」消息，好多香港网民失望之馀，正构思紧搞活动，以示声援。<br />
(译: 广州爆发「粤战」，难得当地年轻人这麽关爱自己本土的语言同文化，八方我作为一直身体力行以广府话写作的一员，当然力挺。但昨天传来内地当局打压原定明日在广州举行的「齐撑粤语大行动」的消息，好多香港网民失望之馀，正构思搞活动，以示声援。)<br />
<br />
广州网民原本发起听日傍晚蘸Ｖ榍江南西地铁站出口集会，呼吁大家到时着白衫，现场仲会玩广州话歇後语游戏，答中有明代名将袁崇焕名句「掉哪妈！顶硬上！」 Tee送，唔少香港人都谂住北上声援。<br />
(译: 广州网民原本发起明日傍晚在海珠区江南西地铁站出口集会，呼吁大家到时穿白衫，现场还会玩广州话歇後语游戏，答γ鞔名将袁崇焕名句「掉哪妈！顶硬上！」 有Tee送，不少香港人都打算住北上声援。)<br />
<br />
广州行动发起人被拘<br />
<br />
不过，x日传来行动发起人盏钡乇恢伟簿辛糌蓟疃取消消息。虽然唔少网民仍然话星期日会照去裢罚行吓街买吓S见吓面呀睿但究竟到时会点尤酥。有香港网民就透过 facebook「我相信可以召集十万人阻止中国推普灭粤的恶行!!!」群组，扬言「大陆搞唔到香港搞」。<br />
(译: 不过，昨天传来行动发起人在当地被治安拘留丶活动取消的消息。虽然不少网民仍然说星期天会去那边，逛街购物见见面之类的，但究竟到时会怎样没有人知道。有香港网民就透过 facebook「我相信可以召集十万人阻止中国推普灭粤的恶行!!!」群组，扬言「大陆搞不成就到香港搞」。)<br />
<br />
O初步定 8月 1日星期日下午两点，胀遄行薅偾虺∑鹈，将行动搬香港，呼吁参加者到时齐着白衫撑粤语罨啊Ｓ型友立即和应，话「强烈支持当日有袁崇焕名句 T-shirt卖」。<br />
(译: 他们暂时定了 8月 1日星期日下午两点，在湾仔修顿球场起锚，将行动搬来香港，呼吁参加者到时候一齐穿白衫挺粤语。有网友立即和应，说「强烈支持当日有袁崇焕名句 T-shirt卖」。)<br />
<br />
香港同系岭南文化发扬之地，好应该力撑广州同胞。虽然障愀郏广东话生命力仍然好顽强，但一人担心，面对两地融合大势所趋，「今日广州，明日香港」，香港流行文化中广东歌丶港产片强势不再，就系例子。<br />
(译: 香港一样是岭南文化发扬之地，好应该力挺广州同胞。虽然在香港，广东话的生命力仍然好顽强，但一些人担心，面对两地融合大势所趋，「今日广州，明日香港」，香港流行文化中的广东歌丶港产片强势不再，就是例子。)<br />
