<strong>不公!正确预测汶川地震专家被迫提前退休</strong><br />
<br />
翟明磊<br />
<br />
壹报连续为抬抬女,冯正虎,被自杀副市长鸣不平,有友人说壹报可改为打抱不平报了。的确壹报以公民教育为主线,打抱不平只是偶尔为之。不过，这一期壹报不得不继续打抱不平，这一不公正事件不仅因为是由于壹报的报道而引起。另重要的是这种不公有可能导致汶川悲剧重演。<br />
<br />
潘正权，德阳地震局的预报员，仅仅因为他正确预报了汶川地震，并通过壹报向外界公开了汶川预报的真相，受到领导报复，于上月被逼提前四年退休，离开了心爱的地震预报岗位。<br />
<br />
国家地震局与四川地震局称汶川地震没有预报，很难预测，这个谎言被一个小小的德阳地震局预报员所打破。<br />
<br />
潘正权是一位成功预报过汉旺地震(被写入官方的《中国震例》)有三十五年地震预报经验的老预报员。2008年3月19日，潘正权得知下面什邡马井镇万兴社区去年十二月份之后出现井水变色。而且水里有青霉素味。潘正权立即在现场调查，并警觉地回想起松潘地震时同样的事情发生过。得出初步结论：隐藏断裂活动引起地下物质渗溢，引起水变色变味，属于地震宏观异常。<br />
<br />
潘正权向省地震局报告这一异常后，省地震局置之不理。情急之下，4月16日潘正权打破阻挠用德阳地震局的名义正式下发文件，宣布德阳地面出现地震宏观异常。这份德阳地震局十八号文件送到市府市委三十个部门，并同抄送给省地震局。<br />
<br />
省地震局没有任何反应，没有上报，也没下来核实。<br />
<br />
左等右等。4月30号德阳地震区金河台数字倾斜，GL值马尔康小金间出现异常。潘正权明白这不是开玩笑了。当天他填写了正式的标准会商卡。<br />
<br />
“确定小金南地区为震区。震级五级。依据为水井，电磁波，倾斜，小震活动分析。时间为5月”。<br />
<br />
这是汶川地震中从龙门山地震带发出的唯一一张地震会商卡，也是龙门山带唯一的短临预报。这也是事后四川省地局反思材料中确认的唯一报宏观异常的两个台站之一。<br />
<br />
也是四川省地震局唯一的一份三要素齐全的预报。<br />
<br />
这个预报时间准确：五月，震级偏小，汶川实际发生为8级地震，报的区域与实际震源相交，小金南地区也是受灾区。这样的预报如果及时防范可大大减少地震损失。这样的预报被中国地震预报权威，地震局局长陈建民的老师汪成民称为正确预报。（不精确，但判断正确）<br />
<br />
令人吃惊的是，四川省地震局没有任何反应。<br />
<br />
5月6日，就是在汶川地震前六天，潘正权发现金河台数据增加三倍，确定无异的地震前征，潘正权又一次上报四川地震局。<br />
<br />
省地震局同样不做任何反应。<br />
<br />
一场死亡八万余人的大地震就发生了。5月12日。<br />
<br />
四川地震局失职！<br />
<br />
四川省局的人在反思会上说“潘正权你是提过意见，但你说的是五级和八级相差两级。”潘正权大声说“总比你们不划圈圈，连个龙门山监视区都不画好些吧！我是黑屋子摸门，起码说有地震，你们连有都没有。”<br />
<br />
听了潘的话。总结时，四川省地震局地震预测研究所程万正哭了，而且哭了好几回。<br />
<br />
原来地震局发现唯一在局里龙门山地震带提出三要素的只有潘正权，唯一报异常的是潘正权。可是一切都迟了，迟了！<br />
<br />
2009年5月，潘正权不顾不明人士的威胁，（威胁是手机匿名短信，原文公布如下：“ 这里面牵涉到上面对整个地震预报的处理，你一定不要开腔，要小心。 ”）勇敢地接受了我的采访，在公民网络媒体壹报公开汶川地震预测真相。称四川省地震局不上报不核查是不作为行为。<br />
<br />
文章发表后，潘正权受到极大压力。德阳地震局局长警告潘正权，“我们已经调查过了，翟明磊不是记者，你私自接受他采访是非法行为！”并且要潘正权停职检查。一天，潘正权面前坐着德阳市地震局正副五位局长，逼迫潘打电话给我要求撤下文章。我拒绝无理要求，并称，在网络时代，人人都是记者。世界范围内的众多文明国家，任何公民都有权对公共事务进行调查，不需要记者证。我是博客不归中宣部管，你们无权要求我撤稿。<br />
<br />
最后为了保护潘正权免受开除失业的命运，我做了部分退让，删去德阳地震局局长装修办公室买轿车等三句话，以免潘得罪顶头上司。<br />
<br />
让步后，德阳地震局局长得寸进尺，要求全文删除。我让潘正权转告，你局长没有权利要求一个公民撤回他的博客文章。并且这个文章是事实的报道。我要求与局长直接通话，他拒绝。我只有请潘转告我的一句话：局长先生，潘正权正确预报汶川地震是国宝。保护国宝，你就是有功之臣，迫害国宝你就是有罪之人。请权衡之。<br />
<br />
由于潘正权告知是在国家地震局邀请的全国会议上接我的采访。所以德阳地震局没有贸然开除潘。<br />
<br />
但自从我汶川地震预测真相系列文章在壹报发表后，局里实际让潘正权处于无事可干的“停职”状态（没有停职的名义，实际上却靠边站）。不能做地震预测分析，甚至不能参加县里的会议，各种参观也不允许。潘，这样一个资深的预报员竟无事可干。<br />
<br />
这样的状态持续三个月后，德阳市地震局于九月份打了报告，10月29日批准，强迫潘正权退休。而潘正权正常退休应当在2013年，整整提前了四年。这样一个经验丰富的地震守望者离开了他的岗位。<br />
<br />
“这是不公正的，与我揭开了汶川地震预测真相有关！我们的一位副局长，就是那位嘲笑我地震预报有什么用，就是他，擅自撤掉了德阳地震局一个关键台站，导致地震预测困难。这样的副局长竟然升一级当调研员！而我一个国家地震局被认为是有功之人被提前退休！这公平吗？”<br />
<br />
我问潘正权：“你后悔吗？后悔向壹报公开汶川预测真相吗？”<br />
<br />
潘大声说：“我绝不后悔！汪成民说：八万人的死应当换来几句真话，我不过是说了真话而已。我不后悔，因为我说的都是实话，我没有一样东西是编造的。2008年2月就发出警告了，地震带我都是圈出来的。”<br />
<br />
更可笑的是，被迫退休后，德阳市地震局局长还让潘正权写保证书，不要上访，不要上告。潘告诉我，面对这种屈辱，“我人在屋檐下，不得不低头”被迫签了这个保证书，这也是他退休后没有及时向外界公布的原因。<br />
<br />
然而他的地震界的朋友们为他打抱不平：“你怕什么。地震预测三十五年，他们还让你做一个主任科员，好比乞丐再沦落也不过是叫花子！”于是潘正权鼓足勇气向壹报诉说。<br />
<br />
他一口气说了下去！<br />
<br />
“官方太黑了，不想干了，是真的。我养老算了，不要管地震了。在这个环境下，地震预测没办法干。干事的人太艰辛了。现在地震局开一个研讨会没人来，开一个有发礼品的会局长副局长都会来。我以地震局名义发布的防震文件，宣布有宏观异常，局长看了非常生气，要求我删了，而且要我把电脑里的也删了。96年以后就给我穿小鞋，2009年五月，我接受你采访揭发汶川预测真相后，他们就实际上停了我的职，也就是不让参与业务活动，甚至连下面的县的地震会议都不让我参加，甚至连参观的会都不让我参加，我等于没事干。<br />
<br />
99年我就成功预测了汉旺地震。我们的局长是原来政协副秘书长。两个副局长一个以前是武装部部长，一个是管理科科长都不懂地震。咋个搞。啥子都不让你干，怎么干？只有退休，退休还能提一级，副县级，多拿几百元。”<br />
<br />
我问“你家里人怎么看？”他的回答出乎意外。<br />
<br />
“我夫人非常欢迎我退休，她说你老得罪人，哪一天被杀了都不知道。”<br />
<br />
512地震，德阳市做得非常完美，这里有一份我的功劳，他们做得太过分。我的功劳是有证可查，有史可查的，我尽了力，国家地震局与四川省地震局的反思材料写得清清楚楚。德阳是报了宏观异常的。”<br />
<br />
最后他用绝望的声音反复地说：<br />
<br />
“ 我下课，我没有办法，我下课，我有什么办法？”<br />
<br />
我深深感到一个地震基层工作者的屈辱，辛酸，虽然在电话里看不到潘的面孔，但从他有些梗咽的话语知道，他已心灰意冷了。<br />
<br />
我请他尽最后的努力为中国地震预测的未来做建言。潘说：“中国地震系统是上下脱节。国家地震局眼里看不起地方上的群测群防，对基层专家深入认识程度不够。强祖基院士的一个女研究生在我这儿学习了一个月。反而是院士还尊重我一些。为什么说四川省地震局有严重的责任要追查呢？汶川震后，我收集龙门地区36个县材料，全部存在气象上的宏观异常。<br />
<br />
2月14日，都江堰出现了二百多群震有的达到三级可感。成都市地震局副局长洪时中在都江堰电视台反复地播：“都江堰不会发生唐山这样的大地震！”地震局应当负责任。首先四川省地震局是收到我宏观异常的，他们既没有往上报，也没有下来查。你如果来查了，上报了，没查出来，或没人管，那是水平问题，而不是态度问题。”<br />
<br />
我说一句最后的实话：现在的人还把持着领导岗位，汶川这样的悲剧还要出现。”<br />
<br />
采访中一位地震界的权威预测者向壹报提出质疑，国家地震局四川地震局不是最迷信小震闹，大震到。这会怎么打马虎眼说是没有大震？这二百多次都江堰小震也被死死掩盖，四川省地震局对外称汶川地震没有一点前震，无法预测。<br />
<br />
是的，这些判断失误，不作为的领导者没有受到任何处置，反而让有功之臣受过。四川省地震局黑白颠倒。历来是先进工作者的潘正权，因为预报了汶川地震，得罪了四川省地震局，2008年没有评上先进工作者。2009年被迫提前退休。<br />
<br />
唐山地震一幕仿佛又重演了。预报唐山要地震的耿庆国，汪成民被审查，靠边站，主张华北无大震的某负责人反而脱离责任，青云直上。在地震界这种劣币淘汰良币的现象已不止一次发生过了。<br />
<br />
做为全程报道地震预测的公民记者，我认为四川省地震局与德阳地震局需要一场人事地震，撤换在汶川预测中不负责任的官员。古人将震卦写成上中各两小横，最下一长横，（相同的两个符号叠加）意为阴气太重，阳气压在下面不得抒发，小人得重位，君子被挤下台。古人认为地震是与政治有关的。这一震卦作为中国地震台网中心的标识。起码在中国地震界，这一震卦名符其实。<br />
<br />
另有北京专家向壹报反映，汶川地震后，四川省地震局本应彻底反思寻找原因，当时中央组织北京专家来四川考察现场。四川省地震局不出面，迎接他们的竟是旅行社导游，在一些点上是走马观花拍照，并不能与当地百姓深入了解实情。有一些专家愤怒地脱开这种走形式的考察团留下来自己调查。<br />
<br />
在这些专家中，潘正权是唯一用脚走完考察全程的人。<br />
<br />
后记：尽管壹报做的是正确的报道，尽管潘正权表示绝不后悔接受了壹报的采访，但我仍为壹报的报道导致潘正权先生提前退休受到污辱与损害而深深内疚。我也非常赞同艾晓明老师说的媒体对被报道者不应当是消费者的心态，消费他的好消息，坏消息。然后置之不理。因此壹报会极力维护潘正权的合法权益，并向各公众媒体呼吁维护一个优秀地震预测者的尊严与利益。壹报会竭尽所能。也请大家相助。的确如果这样的正确预报员仅仅因为他仗义执言就被下课，未来谁敢做真正的地震预报？不预报地震的没事，还有功，预报的反而受过，受辱，受伤有这样不公平的事吗？这样下去，下一个大地震，谁还敢象潘正权一样大声预测呢？我们的安全又何在呢？<br />
<br />
我还呼吁，哀莫大 &#65533;&#65533;心死的潘正权先生不要放弃希望，坚持预测，民间总会有有识之士支持你的！<br />
