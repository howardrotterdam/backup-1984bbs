													<h2>正确预测汶川地震专家被迫提前退休</h2>
						
						
																											<div id="postmessage_380450" class="t_msgfont">作者：翟明磊 | 评论(29) | 标签:汶川地震, 地震预测, 潘正权, 地震局, 四川地震局<br />
