你们真幸运，国家的盛世如此集中相伴在你们大学的记忆中。08奥运留下的记忆，不仅是金牌数的第一，不仅是开幕式的华丽，更是中华文化的魅力和民族向心力的显示；六十年大庆留下的记忆，不仅是领袖的挥手，不仅是自主研制的先进武器，不仅是女兵的微笑，不仅是队伍的威武整齐，更是改革开放的历史和旗帜的威力；世博会留下的记忆，不仅是世博之夜水火相容的神奇，不仅是中国馆的宏伟，不仅是异国场馆的浪漫，更是中华的崛起，世界的惊异；你们一定记得某国总统的傲慢与无礼，你们也让他记忆了你们的不屑与蔑视；同学们，伴随着你们大学记忆的一定还有什锦八宝饭；还有一个G2的新词，它将永远成为世界新的记忆。 <br />
<br />
近几年，国家频发的灾难一定给你们留下深刻的记忆。汶川的颤抖，没能抖落中国人民的坚强与刚毅；玉树的摇动，没能撼动汉藏人民的齐心与合力。留给你们记忆的不仅是大悲的哭泣，更是大爱的洗礼；西南的干旱或许使你们一样感受渴与饥，留给你们记忆的，不仅是大地的喘息，更是自然需要和谐、发展需要科学的道理。 <br />
<br />
在华中大的这几年，你们会留下一生中特殊的记忆。你一定记得刚进大学的那几分稚气，父母亲人送你报到时的情景历历；你或许记得“考前突击而带着忐忑不安的心情走向考场时的悲壮” ，你也会记得取得好成绩时的欣喜；你或许记得这所并无悠久历史的学校不断追求卓越的故事；你或许记得裘法祖院士所代表的同济传奇以及大师离去时同济校园中弥漫的悲痛与凝重气息；你或许记得人文素质讲堂的拥挤，也记得在社团中的奔放与随意；你一定记得骑车登上“绝望坡”的喘息与快意；你也许记得青年园中令你陶醉的发香和桂香，眼睛湖畔令你流连忘返的圣洁或妖娆；你或许“记得向喜欢的女孩表白被拒时内心的煎熬”，也一定记得那初吻时的如醉如痴。可是，你是否还记得强磁场和光电国家实验室的建立？是否记得创新研究院和启明学院的耸起？是否记得为你们领航的党旗？是否记得人文讲坛上精神矍铄的先生叔子？是否记得倾听你们诉说的在线的“张妈妈”？是否记得告诉你们捡起路上树枝的刘玉老师？是否记得应立新老师为你们修改过的简历，但愿它能成为你们进入职场的最初记忆。同学们，华中大校园里，太多的人和事需要你们记忆。 <br />
<br />
请相信我，日后你们或许会改变今天的某些记忆。瑜园的梧桐，年年飞絮成“雨”，今天或许让你觉得如淫雨霏霏，使你心情烦躁、郁闷。日后，你会觉得如果没有梧桐之“雨”，瑜园将缺少滋润，若没有梧桐的遮盖，华中大似乎缺少前辈的庇荫，更少了历史的沉积。你们一定还记得，学校的排名下降使你们生气，未来或许你会觉得“不为排名所累”更体现华中大的自信与定力。 <br />
<br />
我知道，你们还有一些特别的记忆。你们一定记住了“俯卧撑”、“躲猫猫”、“喝开水”，从热闹和愚蠢中，你们记忆了正义；你们记住了“打酱油”和“妈妈喊你回家吃饭”，从麻木和好笑中，你们记忆了责任和良知；你们一定记住了姐的狂放，哥的犀利。未来有一天，或许当年的记忆会让你们问自己，曾经是姐的娱乐，还是哥的寂寞？ <br />
<br />
亲爱的同学们，你们在华中科技大学的几年给我留下了永恒的记忆。我记得你们为烈士寻亲千里，记得你们在公德长征路上的经历；我记得你们在各种社团的骄人成绩；我记得你们时而感到“无语”时而表现的焦虑，记得你们为中国的“常青藤”学校中无华中大一席而灰心丧气；我记得某些同学为“学位门”、为光谷同济医院的选址而愤激；我记得你们刚刚对我的呼喊：“根叔，你为我们做成了什么？”――是啊，我也得时时拷问自己的良心，到底为你们做了什么？还能为华中大学子做什么？ <br />
<br />
我记得，你们都是小青年。我记得“吉丫头”，那么平凡，却格外美丽；我记得你们中间的胡政在国际权威期刊上发表多篇高水平论文，创造了本科生参与研究的奇迹；我记得“校歌男”，记得“选修课王子”，同样是可爱的孩子。我记得沉迷于网络游戏甚至频临退学的学生与我聊天时目光中透出的茫然与无助，他们还是华中大的孩子，他们更成为我心中抹不去的记忆。 <br />
<br />
我记得你们的自行车和热水瓶常常被偷，记得你们为抢占座位而付出的艰辛；记得你们在寒冷的冬天手脚冰凉，记得你们在炎热的夏季彻夜难眠；记得食堂常常让你们生气，我当然更记得自己说过的话：“我们绝不赚学生一分钱”，也记得你们对此言并不满意；但愿华中大尤其要有关于校园丑陋的记忆。只要我们共同记忆那些丑陋，总有一天，我们能将丑陋转化成美丽。 <br />
<br />
同学们，你们中的大多数人，即将背上你们的行李，甚至远离。请记住，最好不要再让你们的父母为你们送行。“面对岁月的侵蚀，你们的烦恼可能会越来越多，考虑的问题也可能会越来越现实，角色的转换可能会让你们感觉到有些措手不及。”也许你会选择“胶囊公寓”，或者不得不蜗居，成为蚁族之一员。没关系，成功更容易光顾磨难和艰辛，正如只有经过泥泞的道路才会留下脚印。请记住，未来你们大概不再有批评上级的随意，同事之间大概也不会有如同学之间简单的关系；请记住，别太多地抱怨，成功永远不属于整天抱怨的人，抱怨也无济于事；请记住，别沉迷于世界的虚拟，还得回到社会的现实；请记住，“敢于竞争，善于转化”，这是华中大的精神风貌，也许是你们未来成功的真谛；请记住，华中大，你的母校。“什么是母校？就是那个你一天骂他八遍却不许别人骂的地方”。多么朴实精辟！ <br />
<br />
亲爱的同学们，也许你们难以有那么多的记忆。如果问你们关于一个字的记忆，那一定是“被”。我知道，你们不喜欢“被就业”、“被坚强”，那就挺直你们的脊梁，挺起你们的胸膛，自己去就业，坚强而勇敢地到社会中去闯荡。 <br />
<br />
亲爱的同学们，也许你们难以有那么多的记忆，也许你们很快就会忘记根叔的唠叨与琐细。尽管你们不喜欢“被”，根叔还是想强加给你们一个“被”：你们的未来 “被”华中大记忆！（见习记者 朱建华 通讯员 王潇潇 实习生 朱芮）</div>

							
							
							
							
