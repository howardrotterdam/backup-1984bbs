<br />
昨日，华中科技大学光谷体育馆，7780名本科毕业生参加毕业典礼，人数之多全国罕见，被称作“最牛毕业典礼”。 （记者许少峰 陈勇摄）<br />
<br />
<br />
<br />
图为：毕业生争相和校长李培根院士合影 记者陈勇许少峰摄<br />
<br />
<br />
<br />
毕业典礼让不少学生落泪<br />
<br />
<br />
<br />
本报讯 昨日，在华中科技大学2010届本科生毕业典礼上，校长李培根院士16分钟的演讲，被掌声打断30次。全场7700余名学子起立高喊：“根叔！根叔！”<br />
<br />
●李培根讲话成为众多师生当天网络签名<br />
<br />
在2000余字的演讲稿中，李培根把4年来的国家大事、学校大事、身边人物、网络热词等融合在一起。“俯卧撑”、“躲猫猫”、“打酱油”、“妈妈喊你回家吃饭”、“蜗居”、“蚁族”、“被就业”、“被坚强”……都是李培根昨日演讲中出现的词汇。<br />
<br />
该校能源学院鲍永杰说：“没想到校长会这么亲切。”<br />
<br />
“什么是母校？就是那个你一天骂他八遍却不许别人骂的地方。”这句引用的话经李培根在毕业典礼上说出后，昨日成为该校众多师生的网络签名。”<br />
<br />
●5月底从国外回来的飞机上写出初稿<br />
<br />
“校长要用心讲话。”李培根告诉记者，“如果演讲稿由其他人代劳，文采可能会比我好，但不能代表我与学生的讲话。”<br />
<br />
此次演讲稿系李培根本人撰写，“在5月底从国外回来的飞机上，已写出初稿”。<br />
<br />
邵宇平1个月前因踢“毕业杯”足球赛造成右腿骨折。昨日，他拄着拐杖参加了毕业典礼。他说：“‘根叔’讲的很贴近我们，没有官腔，很多都是我们经历过的事情。”<br />
<br />
北京理工大学78岁退休教授杨宗基，昨日专程到汉参加了孙子杨奕的毕业典礼。他认为，李培根校长的演讲很实在，讲到了学生们的心里。<br />
<br />
●“根叔”称谓最初可能出现在论坛上<br />
<br />
华中科大校长李培根院士现在被该校学生习惯性地称为“根叔”。<br />
<br />
昨日，在该校本科生毕业典礼上，7000余名学子多次现场高呼“根叔”、“根叔”。<br />
<br />
在副校长张晋的印象中，“根叔”的称谓最初可能出现在该校白云黄鹤论坛上。“听起来很亲切，是学生对校长的一种‘尊称’”。<br />
<br />
李培根表示，“根叔”的称谓到底从何而来，他也没什么印象，但出现应该有好几年了。现在，不论是学生在公开还是在私下场合这样相称，李培根都会欣然应答。<br />
<br />
●3小时里与1200名应届毕业生一一握手<br />
<br />
昨日，华中科大6个院系的1200余名应届毕业生，在毕业前与“根叔”来了一次亲密接触。<br />
<br />
在约3个小时内，身着导师服的李培根与他们一一握手、合影，其中的百名学生还与他相拥。<br />
<br />
外语学院应届毕业生刘艳丽是昨日与“根叔”拥抱者之一。“大学4年一定要抱一下‘根叔’”，刘艳丽如愿以偿。当她走下舞台时，班上另几名女生纷纷过来与她拥抱，希望借此拥抱“根叔”。<br />
<br />
《记忆》<br />
<br />
――李培根昨日演讲稿摘录<br />
<br />
●曾经是姐的娱乐，还是哥的寂寞？<br />
<br />
我知道，你们还有一些特别的记忆。你们一定记住了“俯卧撑”、“躲猫猫”、“喝开水”，从热闹和愚蠢中，你们记忆了正义；你们记住了“打酱油”和“妈妈喊你回家吃饭”，从麻木和好笑中，你们记忆了责任和良知；你们一定记住了姐的狂放、哥的犀利。未来有一天，或许当年的记忆会让你们问自己，曾经是姐的娱乐，还是哥的寂寞？<br />
<br />
你也许记得青年园中令你陶醉的发香和桂香，眼睛湖畔令你流连忘返的圣洁或妖娆；你或许记得“向喜欢的女孩表白被拒时内心的煎熬”，也一定记得那初吻时的如醉如痴。<br />
<br />
●我也得时时拷问自己的良心，到底为你们做了什么？<br />
<br />
我记得你们的自行车和热水瓶常常被偷，记得你们为抢占座位而付出的艰辛；记得你们在寒冷的冬天手脚冰凉，记得你们在炎热的夏季彻夜难眠；记得食堂常常让你们生气。我当然更记得自己说过的话：“我们绝不赚学生一分钱。”也记得你们对此言并不满意。但愿华中科大尤其要有关于校园丑陋的记忆。只要我们共同记忆那些丑陋，总有一天，我们能将丑陋转化成美丽。<br />
<br />
我记得你们刚刚对我的呼喊：“根叔，你为我们做成了什么？”――是啊，我也得时时拷问自己的良心，到底为你们做了什么？还能为华中科大学子做什么？<br />
<br />
●什么是母校？就是那个你一天骂他八遍却不许别人骂的地方<br />
<br />
请记住，未来你们大概不再有批评上级的随意，同事之间大概也不会有如同学之间简单的关系；请记住，别太多地抱怨，成功永远不属于整天抱怨的人，抱怨也无济于事；请记住，别沉迷于世界的虚拟，还得回到社会的现实；请记住，“敢于竞争，善于转化”，这是华中科大的精神风貌，也许是你们未来成功的真谛；请记住，华中科大，你的母校。“什么是母校？就是那个你一天骂他八遍却不许别人骂的地方”(引自白云黄鹤BBS)。多么朴实精辟！<br />
<br />
●你们的未来“被”华中科大记忆<br />
<br />
亲爱的同学们，如果问你们关于一个字的记忆，那一定是“被”。我知道，你们不喜欢“被就业”、“被坚强”，那就挺直你们的脊梁，挺起你们的胸膛，自己去就业，坚强而勇敢地到社会中去闯荡。<br />
<br />
亲爱的同学们，尽管你们不喜欢“被”，“根叔”还是想强加给你们一个“被”：你们的未来“被”华中科大记忆！<br />
