													<h2>校长毕业典礼演讲串网络热词 7700学子高喊根叔</h2>
						
						
																											<div id="postmessage_701036" class="t_msgfont">2010年06月24日00:44荆楚网朱建华 等我要评论(296) 字号：T|T <br />
