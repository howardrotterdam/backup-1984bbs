2009-03-24 09:50:42 原文地址: <a href="http://blog.sina.com.cn/u/49142d110100crcq" target="_blank">http://blog.sina.com.cn/u/49142d110100crcq</a> <br />
<br />
文/一位藏人<br />
<a href="http://woeser.middle-way.net" target="_blank">http://woeser.middle-way.net</a><br />
<br />
这个令人窒息的国家啊！<br />
<br />
为丹达哭，为他的母亲所经历的苦难痛哭，被单蒙面，为只能哀哭而耻辱。<br />
<br />
只有在多数的汉人知道自己的国家是个什么生态，对历史、现状、文化、政府有所反思，并做出改变才有希望。否则，其他的民族，周边的人，只能被这个政府，以及组成这个政府的汉人民众裹挟着，如同在一条烂船上，一起沉陷。<br />
<br />
<strong>其实，我甚至对这种汉人了解事实后会反思和改变都不抱希望！信息再被封闭，有了解的渠道，人心是肉长的，有些常识，有些疑问，不需要高深的知识和过人的胆识。但汉人心态之漠然，或残暴，或当看客，哪里有希望！</strong><br />
<br />
之前，我把政府和民众是分开来看的。民众，不管什么民族，只要不当权，受政府在土地、权益、福利上的侵害，差不多一样。民众，老百姓，知识分子，学生，不是和我一样的人吗？也许生活习惯不同，衣服不同，起名方式不同，但本质上一样啊。<br />
<br />
<strong>但目睹这些证据，以及族人50年以来的遭遇，对比从去年开始变本加厉的来自一般汉人民众和年轻人的疯狂，不得不得出结论：我们面对的不仅仅是一个政府，还有1949年以来中国人发展到极致的“吃人的文化”。它也自噬。而这个政府，是有纵容它的土壤。他们相辅相成。</strong> 汉人文化中的恶：漠然、自利、暴力等在49年以来的一次次运动中被培育、发酵；而善的那些：仁、礼、道、德被嘲笑，被掐灭。中国政府是在虚假、残暴、贪婪、自利为特色的中国汉人文化土壤上的恶之花。<br />
<br />
偶然出现的，稀少珍贵，犹如晨星般的，善良有勇气的汉人在哪里？在监狱里，或被监控，成为“异议”分子。可惜大多数人不知，也不关心。在这个奇怪的国家，“自由、民主、服务、爱心、理想、体恤、利他……”这些概念，价值观被政府割裂、异化，被民众嘲讽，或认为远不可及。眼瞎了一样，耳聋了一样，空了心的中国人。<br />
<br />
中国人，只有认清自己面目，看清自己政府暴力的来源和实质，才会对丹达和扎白事件开始了解。<br />
<br />
那失去草原的蒙古人，逼成“恐怖分子”的维吾尔人，“分裂分子”加“恐怖分子”的藏人， 面目模糊的各式花瓶族，我们因为在这个国家，说倒底就是因为不肯那么容易放弃土地，不肯那么容易放弃信仰，不肯那么容易放弃生活方式和道德观，不肯那么容易变成汉人，就成了“问题”和“分子”！！所以，哪来的“少数民族问题”？分明是“汉族的问题”，“中国的问题”。<br />
<br />
<strong>看见这些证据还叫“杀得好”，叫“活该”的，或者明明知道那边媒体不得进，言论完全受控封杀，还要求“第三方独立证据”的，不是一个又一个人吗？那虐杀丹达的，枪击自焚扎白的，不是一个又一个由父母养育的人吗？什么样的民族，什么样的文化，什么样的父母，会孕育这样的人民？！</strong><br />
<br />
注释：<br />
<br />
【1】丹达：拉萨人，30岁，被虐杀之前是西藏移动公司的职员。2008年3月14日，为救被中国武警毒打的年迈老僧，他恳求武警手下留情，却被那些武警毒打，而后被抓走，受尽令人震惊的酷刑，不得而治，于6月19日，北京奥运火炬在拉萨传递的前一天惨死家中。<br />
<br />
【2】扎白：安多阿坝（今四川省阿坝州阿坝县）格尔登寺僧人。24岁。2009年2月27日，为抗议当局对宗教的压制，他当街自焚，被军警枪击。目前生死不明。中国官媒否认枪击，称在成都某医院医治。来自当地的消息指他面临双腿被截肢，疑是被掩盖枪击证据。 <br />
