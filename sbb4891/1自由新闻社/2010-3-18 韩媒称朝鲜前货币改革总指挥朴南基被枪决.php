																											<div id="postmessage_521886" class="t_msgfont">朝鲜货币改革让农民矿工成为社会高收入者<br />
<br />
&nbsp;&nbsp;摘自：环球时报 <br />
<br />
朝鲜货币改革后，给工人发放同货币改革之前同数额的薪酬，由于新旧币按照1新币=100旧币兑换，实际上大大提升了工人工资。据《朝鲜新报》1月4日报道，新年假期，朝鲜商场内高价消费品的购买人群以农民、矿工为主。 <br />
<br />
　　报道称，新年第一天，从凌晨1点钟就有人在朝鲜平壤最大的商场--平壤第一百货店门前等候商场营业，为此商场特意将营业开始时间由原来的10点钟提前到了早上7点。即便如此，在下午3点钟左右，因人数过多，为防止意外不得不实行了临时的入场限制。 <br />
<br />
　　据商场负责人郑明玉（音）介绍，从事重体力劳动的农民、矿工等是“高收入人群”。他们是电视、洗衣机、冰箱等高价商品的主要购买者。而一般工人则购买食品、糖果、一般日用品居多。她表示：“这正是社会主义多劳多得分配制度贯彻实施的体现。”看到商场内货架上摆满了各种商品和争相购买的消费者，商场的销售人员都感叹道：“强盛大国的大门已经离我们不远了。” <br />
<br />
　　在兄弟山地区农场工作的李金玉（音）兑换到了5万元的新货币，她说：“农场里的人都要买买彩色电视机。”她还用1500元的新币购买了以前售价5000-7000元的毛毯。 <br />
<br />
　　在三石地区农场工作的李花淑（音）购买了电视、衣被、毛毯等物品，她说：“金正日将军为我们创造了今天的幸福生活，作为报答，我要买一些米和猪肉去支援熙川发电站的建设。” <br />
<br />
　　据统计，当天商场共销售了朝鲜国产电视155台，毛毯550多张。</div>

							
							
							
							
