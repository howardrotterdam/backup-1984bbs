																											<div id="postmessage_472321" class="t_msgfont">（中央社台北8日电）习近平和李克强是中共内定第五代领导人，习近平的妻子彭丽媛家喻户晓，李克强的妻子是谁，外界知之甚少，非常神秘。最新一期「多维月刊」披露，李克强夫人程虹是位学者，研究美国文学。<br />
<br />
一般认为，李克强未来将接替温家宝，担任中国国务院总理，习近平则会成为中共总书记。果真如此，程虹有朝一日将成为中国的总理夫人。<br />
<br />
报导说，程虹现年53岁，出生于郑州一个干部家庭，父亲程金瑞曾任共青团河南省委副书记，母亲刘益清是新华社记者。程金瑞后来调北京，担任中国国务院扶贫开发办公室顾问，为副部级干部。<br />
<br />
文化大革命期间，程虹一度到农村「插队」，后来在北京清华大学进修英文，在几位热心朋友的介绍下，与大她两岁的北京大学团委书记李克强成婚，如今两人已共度26年时光。<br />
<br />
报导说，程金瑞对李克强后来在共青团中央的崛起，发挥了一定作用。而程虹则拿到了中国社会科学院文学博士，长期在北京首都经贸大学外语系任教。<br />
<br />
报导指出，程虹和中共其它高官夫人热中交际或商场不同，她更喜欢校园的宁静，对于教学也充满热忱，曾经两度获选为学校「我心目中的十佳教师」。<br />
<br />
当前，程虹是首都经贸大学学术委员会委员。校方一直有意提拔她担任系主任甚至更高的职位，但都被程虹拒绝。程虹不喜欢繁琐的行政事务，以及无聊的应酬，当然也不希望被人说是「妻凭夫贵」。<br />
<br />
报导表示，李克强进入中南海担任常务副总理之后，程虹基本不 上讲堂，主要做研究工作。<br />
<br />
程虹曾经在美国布朗大学担任访问学者，著作等身，号称中国从事美国自然文学研究的第一人，是中国大陆这个领域顶尖的专家。她翻译了美国自然文学经典「醒来的森林」、「遥远的房屋」，还出版了大陆第一部系统介绍美国自然文学的著作「寻归荒野」。<br />
<br />
李克强与程虹育有一女，毕业于北京大学，现在美国留学。<br />
<br />
报导说，程虹相当低调，但绝对上得了台面，她说得一口娴熟的英语，「如果参加外事活动，肯定是李克强的软实力」。</div>

							
							
							
							
