													<h2>飞机上打手机</h2>
						
						
																											<div id="postmessage_705300" class="t_msgfont"><font color="red">看错的人忏悔吧</font><br />
<img src="http://i3.6.cn/cvbnm/0b/3b/5d/0c08efa2aa90daa93a45a7b3382b82d1.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
成都商务客认为话费偏贵，普通商务电话不会在飞机上拨打<br />
<br />
　　乘坐飞机必须关闭手机，这个规定有望被打破。日前在上海举行的中国航空运输协会与各航空公司的机上通讯研讨会上，国航、南航、东航纷纷表示已和移动运营商签署了机上通讯软件的开发协议，并已呈报政府部门审批。一旦审批通过，乘客在机上可用手机打电话，收费为每分钟15元左右。至于服务何时对乘客开放，记者昨日采访三大航相关人士获悉，具体开通时间暂无时间表。另外，到底是用自己的手机，还是使用机上提供的设备，目前记者没有从航空部门了解到更详细的信息。<br />
<br />
　　回溯<br />
<br />
　　深航曾想试水但未获批<br />
<br />
　　中国航空运输协会透露，国内两家移动通讯运营商已于去年7月、8月、9月份分别与国航、南航、东航签订了机上手机通讯软件开发的框架性协议，并将经营许可权的申请上报给国家经信部和中国民用航空局。监管部门批准后，就可以具体实施。<br />
<br />
　　国航客舱服务部副总经理王晓伦认为，是否能实现在万米高空中通话，会成为航空公司吸引旅客的最有力手段之一。但价格确实较贵。上航举例说，如果在中国上海至美国的航班上开展机上通话服务，每分钟大概收费15元左右，比地面越洋电话还贵2至3倍。<br />
<br />
　　实际上，国内航班的机上手机通讯探索早就开始。2007年，深圳航空宣布将与瑞士OnAir公司合作，推动在深航班机上实现自由通信，不过，这一项目最终没有获得审批。<br />
<br />
　　昨天，记者就此事采访了国航、东航两家航空公司。国航宣传部表示，目前尚没有接到来自公司业务部门的通知。东航也没有给出确切的答复。<br />
<br />
　　但在5月15日举行的上海世博会“信息化与城市发展”主题论坛上，中移动董事长王建宙表示，在飞机上用手机打电话，在技术上是能实现的。中国移动正在就这项业务与航空公司商谈。<br />
<br />
　　反应<br />
<br />
　　成都商务客认为话费偏贵<br />
<br />
　　以每分钟15元的高昂费用拨打手机，是否会获得市场接受？昨天，记者就这个问题采访了多名成都商务人士。他们均表示，这样的服务的确是方便了乘客。但如果是不太紧急的情况，还是没有必要。<br />
<br />
　　经常出差的旅行社副总孟先生表示，一般的国内航线飞行时间就两三个小时，大部分的事情都不急于在这两三个小时来处理。国航西南分公司客舱部一名工作人员说，从她的日常工作来看，有很多商务人士，手机通讯非常频繁。如果开通机上<br />
<br />
　　手机通讯业务，应该赢得一些市场，尤其是在头等舱和商务舱。<br />
<br />
　　在现实的航班中，旅客需要解决的紧急状况时有发生。2006年，南航从深圳起飞的航班前往乌鲁木齐，就在起飞半小时后，一位青年男子称上飞机前在家里煲汤忘记了关煤气！此时他的妻子正在上班，而煤气炉如果无人照看，一直烧下去可能引起火灾或煤气爆炸。了解情况后，航班机长决定破例帮王先生在万米高空打卫星电话，通知王先生的妻子赶紧回家关煤气炉。而这时锅都已烧焦变形了。记者陶颖<br />
<br />
　　链接<br />
<br />
　　外航的机上通话<br />
<br />
　　早从2000年起，在飞机上打手机的梦想就已实现。在英国维珍航空公司由上海飞往伦敦班机上，乘客成为第一批被允许在飞行途中使用他们的手机进行通话的客人，这在全球航空公司中尚属首例。这项最新的空中电话服务技术配备了英国电信开发的机载移动通讯系统，其电话费用将自动转入用户的全球通手机账号上。乘客只需在登机后，将其SIM卡放置在飞机上专门提供的手机上，输入注册号，便可自由通讯了。<br />
<br />
　　OnAir公司的机上通话，其原理也是机上安装移动通讯系统，相当于在机舱内模拟一个本地的GSM网络。机舱内的手机、电脑在使用时将不会连接地面网络，而是首先通过机载卫星通讯设备传输到国际通讯卫星，卫星再将信号传输到地面基站，继而通过基站完成与地面通讯设备之间的数据交换。当飞机飞行高度低于海拔3000米时，机载通讯设备可以自动抑制乘客的机上通信，以保障飞机在起飞和降落时正常通讯和设备不受干扰。</div>

							
							
							
							
