													<h2>国内航企欲试水飞机上打手机 预计每分钟15元</h2>
						
						
																											<div id="postmessage_700557" class="t_msgfont">中国网6月24日报道 “航班已经进入平飞状态，请将您的手机保持在关闭状态。”这句经典的飞行航班常用提示语，或许将退出历史舞台。<br />
<br />
在昨日中国航空运输协会与各航空公司的机上通讯研讨会上，国航、南航、东航三大国内航空公司表示已经和移动运营商签署了机上通讯软件的开发协议，并已呈报相关政府部门审批。一旦审批通过，乘客在机上用手机可自由打电话，收费为每分钟15元左右。<br />
<br />
已通过安全实验<br />
<br />
由于技术等方面的原因，中国航班上至今不允许、也没有条件使用手机、互联网。专家解释说，日常使用的手机之所以无法在天上直接使用，主要是因为手机运营商的地面信号达不到万米高空。在国外，有少数航空公司向旅客提供通过海事卫星电话与地面联系的增值服务以弥补这项缺憾。但中国交通通信中心的与会专家认为，这种海事电话不可能向全体旅客提供，“一是代价昂贵，二是缺乏庞大容量。”<br />
<br />
他描述说，迄今为止，全球已经开发出多种技术平台，可以把地面的手机基站信号和天上的卫星信号连接起来。“就好比用固定电话拨打IP电话，或者在电脑上通话。”当然，飞机必须进行改装，增添相应的设备。<br />
<br />
正等待政府部门批准<br />
<br />
中国航空运输协会透露，国内两家移动通讯运营商已经于去年7月、8月、9月份分别与国航、南航、东航签订了机上手机通讯软件开发的框架性协议，并将经营许可权的申请上报给国家经信部和民航局。监管部门批准后，就可以具体实施。<br />
<br />
事实上，深航曾经与瑞士OnAir公司在2007年签订合约，成为中国首家应用个人手机机上通讯技术的航空公司。但最终，项目未获审批。中国航空运输协会专家坦言，航空公司和通讯运营商取得政府的审批，是此项业务能否真正推广的前提。<br />
<br />
通话费比地面贵2至3倍<br />
<br />
<br />
国航客舱服务部副总经理王晓伦指出，一些常旅客在机舱关门前，需要空姐一再提醒关闭手机。这说明，他们需要随时与外界联系。他认为，是否能实现在万米高空中通话，会成为航空公司吸引旅客及令旅客得到满意服务体验的最有力手段之一。<br />
<br />
专家认为，国外航空公司向旅客收取的机上通话费是每分钟1美元以上，“大约10元人民币，相比海事电话的1分钟可能50元人民币，还是便宜的。”该专家表示，机上上网费可以按照流量收取。<br />
<br />
上航举例说，如果在中国上海至美国的航班上开展机上通话服务，每分钟大概收费15元左右，起码比地面越洋电话贵2至3倍。（来源：东方早报）<br />
<br />
(本文来源：中国网 作者：记者李萌)</div>

							
							
							
							
