																											<div id="postmessage_241862" class="t_msgfont">新闻来源: 阳光报 于September 14, 2009 13:15:43<br />
<br />
8月上旬，宁强县中医院肛肠科医生李某在门诊科室给一女患者进行痔疮检查和治疗中，多次将该患者强奸。目前，李某已被宁强县公安局刑事拘留。<br />
<br />
医生诊室三次强奸患者<br />
<br />
　　8月3日，家住宁强县代家坝镇的宁强某中学17岁的高二女生小兰（化名）因患痔疮一人到宁强县中医院肛肠科就诊，该科室主治医生李某以给小兰检查治疗为名，对小兰进行了强奸。强奸后，李某让小兰当场服下避孕药，但他对小兰却谎称说是治痔疮用的指定药。随后，在8月4日至6日的连续3天检查治疗中，李某用同样的方法又对小兰进行了3次强奸，在每次强奸后，李某都给小兰服了避孕药。小兰回到学校后,感觉到在最后一次检查治疗中李医生的手多次触摸她的隐私部位,就将在中医院肛肠科治疗的过程告诉了她的密友，朋友告诉说她可能受到了性侵犯，涉世未深、单纯的小兰这才意识到当初李医生对自己痔疮进行检查和治疗放药的过程中已经将自己强奸。随后小兰就去找李某，可李某却不承认，还说小兰诬陷他。<br />
<br />
　　在小兰找李某理论之后，心虚的李某便向医院请了假。随后两天，小兰与父亲找中医院领导时，医院这才得知情况，并立即向有关部门进行报告。警方侦查取证后将李某控制。经审讯，李某对自己多次强奸小兰的行为供认不讳,随后李某被警方刑事拘留。<br />
<br />
　　诊室在二楼比较偏僻<br />
<br />
　　日前，记者到宁强县中医院采访时发现，该院肛肠科位于门诊办公楼左侧的一幢两层楼的二楼，位置比较偏僻，但在楼道口的招牌却非常醒目。在这层楼上除一个供应室和医疗废物回收室外，就只有肛肠科一个门诊科室。记者看到，二楼肛肠科的门已被警方封闭，二楼楼道里晾着洗了的床单和被套，肛肠科科室门上贴的纸条上写着主治医生李某的名字和一个手机号码。该院一位赵姓副院长告诉记者，供应室和医疗废物回收室工作人员只需早晚各去一次，肛肠科由于是个特色科室，平时病人也比较少，所以单独设在二楼。<br />
<br />
　　记者从该院一位工作人员处了解到，李某平时不爱说话，今年31岁，1996年8月中专毕业后进入宁强县中医院肛肠科工作至今，目前职称是中医师。<br />
<br />
　　采访中，宁强县中医院吴副院长对记者说：“医院也是在患者和家属来找到医院反映问题时才知道这事，我们也立即向有关部门报告了，由于李某当时请假在西安，我们打电话叫他才回来的。8月10日，李某回来后就被警察带走了。”<br />
<br />
　　检查异性隐私部位须避嫌<br />
<br />
　　记者询问男医生单独检查女性患者隐私部位是否合乎有关规定时，该院的张院长告诉记者，按照有关规定，医生对女性患者隐私部位进行检查时，应该有家属陪护，如果没有家属陪护，大夫应该叫护理人员在场。张院长还说；“他（李某）可能早就有不良企图，所以没有按制度执行，也没有叫护理人员。”<br />
<br />
　　记者在宁强县公安局了解到，目前李某已被刑事拘留，案件还在进一步深挖中。</div>

							
							
							
							
