<br />
而此次在岗职工平均工资统计资料包括国有单位、城镇集体单位以及联营经济、股份制经济、外商投资经济、港澳台投资经济等单位，统计内容有计时工资、计件工资、奖金、津贴和补贴、加班加点工资及生病等特殊情况下支付的工资。工资总额统计的是个人税前工资。<br />
<br />
另外，今年统计部门还把私营企业纳入了统计范围，公布了「城镇私营单位就业人员年平均工资」。</div>

							
							
							
							
