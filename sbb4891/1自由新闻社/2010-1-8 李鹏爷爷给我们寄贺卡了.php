<br />
　　信里装着2010年的新年贺卡。这是去年12月29日，全国人大原委员长李鹏及其夫人朱琳，特意寄给万州区长岭中心小学师生们的。贺卡的内页，有他们亲笔题写的新年寄语：“国运昌盛，阖家幸福”，表达了李鹏夫妇对三峡库区教育的关怀和对师生们的问候。<br />
