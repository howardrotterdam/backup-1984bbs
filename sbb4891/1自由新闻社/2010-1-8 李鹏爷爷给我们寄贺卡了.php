<br />
　　“李鹏和朱琳两位领导，多年前就给了我校巨大支持！”长岭中心小学校长牟向阳说，1997年，由国家关工委原主任朱琳引荐，香港潘汪惠萍女士通过国家关工委捐资50万港币，在原长岭一校修建起万州长岭中心小学，于1998年6月建成投入使用。10多年来，朱琳一直很关心学校发展，多次向学校赠送《关心下一代》杂志。<br />
<br />
　　“如今，两位领导同志仍然心系三峡库区的师生，让我们深受鼓舞，这是最好的新年礼物！”当天下午，长岭中心小学便召开了全校师生大会，宣读了李鹏和朱琳夫妇寄来的贺卡贺词，全校师生鼓掌欢呼。<br />
<br />
　　六年级一班学生张艳玲说：“李鹏爷爷、朱琳奶奶特意从北京给我们寄来新年贺卡，太让人感动了！我们只有好好学习，才能对得起这份关怀！”（记者 罗静雯）<br />
