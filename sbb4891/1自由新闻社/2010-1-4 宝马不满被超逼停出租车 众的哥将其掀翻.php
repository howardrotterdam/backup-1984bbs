[提要] 1月3日凌晨，四川乐山市繁华地点，46岁的出租车司机李文君与宝马车主产生纷争，宝马车上满嘴酒气的年轻男子一拳打在李文君脸上。现场很快聚集了许多围观者和出租车。的哥们发现自己的同行受了气，将火气撒到了宝马男身上，合力将宝马车掀翻。[<br />
 <br />
宝马车被当街掀翻 <br />
<br />
　　事发乐山市区，当事的哥称未主动找人帮忙，警方已对事件展开调查<br />
<br />
　　车水马龙的主干道上，一辆崭新的宝马5系轿车四轮朝天，道路两旁停满了出租车……昨日凌晨零时许，这一幕出现在乐山市区牛桥附近的嘉定路路段。目击者说，事情缘于一辆出租车和宝马车的纷争，出租车“受气”后引来了更多的出租车，众的哥和现场围观者一齐将宝马车翻了个底朝天。<br />
<br />
　　按喇叭超车 出租车惹怒宝马<br />
<br />
　　李文君今年46岁，他在乐山市开了3年多出租车。昨日凌晨，他从海棠路口送一名女乘客前往汽车联运站附近。经嘉定路行驶至牛桥附近时，前方出现一辆车牌号为“川LA53××”的宝马5系轿车。“它跨着分道线，开得很慢。”李文君说，他当时按了两声喇叭，宝马车闪着应急灯回到了快车道上，他则从慢车道开始超车。就在出租车快要超过时，宝马车突然加速，然后往慢车道上靠。慢车道旁边是绿化带，李文君只好踩了刹车，然后很快被逼停。<br />
<br />
　　李文君说，宝马车上下来一名年轻男子，戴着一圈金项链。“你按啥子喇叭？”该男子气势汹汹地问他，满嘴酒气像是喝高了。“你开个宝马有啥子了不起嘛？速度跑不起来就靠边。”李文君说，他也毫不示弱地回应对方，或许是这句话惹怒了对方，该男子忽然一拳打了过来，结结实实打在他脸上。男子动手后，宝马车上又下来一名年轻女子，出租车上的女乘客也出言相劝，但年轻男子并未罢休。<br />
<br />
　　的哥扎场子宝马被当街掀翻<br />
<br />
　　事发地点位于繁华市区，现场很快聚集起了围观者。直到有人将年轻男子拉开，李文君才脱身下了车，然后打电话报了警。现场的车越来越多，其中有许多是出租车。<br />
<br />
　　“发现自己的同行受了气，的哥们将火气撒到了&quot;宝马男&quot;身上。”现场目击者说，多名的哥围住从宝马车上下来的那名男子，然后拳脚相向。目击者们猜测，这些的哥如此迅速赶到现场，可能是李文君通过车载电台呼救。但李文君对此予以否认，并表示自己车上并未安装车载电台，的哥们应该是从此路过恰好遇见。“不管人是谁喊来的，反正他们动手打了人，而且连女人都打。”宝马车上的年轻女子说，她在上前劝解时也挨了打。<br />
<br />
　　让人没想到的是，围观者的怒气不仅发泄到那名男子身上，宝马车也遭了殃。“把他的宝马放倒……”不知是谁吼了一句，众的哥和一些围观者冲向了宝马车，合力将宝马车翻了个底朝天。<br />
<br />
　　细节待认定事情正在调查中<br />
<br />
　　事发后，警方和120救护车赶到了现场。李文君被送往医院急救，除身上多处软组织损伤外，左手手掌还有一处划伤。在民警的维护下，现场秩序很快平静下来，沿街两旁的出租车也陆续驶离现场。凌晨2时许，宝马车被拖走，现场交通恢复了畅通。<br />
<br />
　　警方相关负责人昨日下午表示，警方已对相关人员作了笔录，经过调查已经初步确认，宝马车驾驶员为年轻女子，该女子并未饮酒。但李文君在采访中坚称，宝马车的驾驶员是那名年轻男子，“只有喝了酒的男人才那么冲，如果是年轻女子开车把我逼停，她后来又为什么会主动过来劝架？”至于李文君是否主动叫人来帮忙、掀翻宝马车的是哪些人等细节，警方表示还将进一步调查。华西都市报<br />
