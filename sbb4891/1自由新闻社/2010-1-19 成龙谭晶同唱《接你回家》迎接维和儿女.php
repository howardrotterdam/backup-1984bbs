													<h2>成龙谭晶同唱《接你回家》迎接维和儿女</h2>
						
						
																											<div id="postmessage_439915" class="t_msgfont">中新网北京1月18日电 (记者 应妮)“接你回家，我不能没有你，尽管你留在那里却铭记在我心里；接你回家，温暖你的土地，永远守候你一生对全世界的情义。”一曲写给中国维和部队八勇士的《接你回家》，代表全中国人民发出了对八位海地维和英雄的呼唤，歌曲由《生死不离》组合王平久和舒楠创作，目前已确定谭晶和成龙共同演唱，1月18日晚将完成录制工作，静待19日上午八位维和英雄的“回家”。 <br />
<br />
　　钟荐勤，女儿出世半年还未曾见过一眼，还等着他起名的“不称职”的爸爸；和志虹，维和部队的翻译官，遇难官兵中唯一的女兵；李晓明，曾参与过北京奥运会火炬护送，维和足迹遍及海地、阿富汗、东帝汶、科索沃等七个任务区的“超级战士”；朱晓平、郭宝山、王树林、赵化宇、李钦……当这些陌生却在此刻无比崇高的名字出现在电视荧幕上的时候，当他们生前的影像资料出现在人们视野中的时候，泪水，模糊了每个人的双眼。<br />
<br />
　　就在8名维和警察“回家”的路途中，一首专为他们而作的歌曲《接你回家》也悄然诞生。曾经为汶川大地震创作歌曲《生死不离》的王平久、舒楠，再次携手，为8位英雄创作了这首《接你回家》。<br />
<br />
　　“这首歌和《生死不离》不一样，我们希望通过这首歌去传递一种力量，让世界去看到我们中国为维护世界和平所做的努力。我们不仅是要去纪念这8名维和部队的警察，而且要传播他们维护和平的精神，去继承他们的遗愿”，词作者王平久在谈到自己填词的初衷时如是说。8名遇难维和警察中的李晓明，曾在去年北京奥运会期间和王平久共事，当时他们还约定：每年北京奥运会纪念日期间再相见。当海地大地震，包括李晓明在内的8名维和警察下落不明时，认识李晓明的北京奥组委成员们都在为他祈祷，期盼奇迹的出现。然而，奇迹没有发生，李晓明的音容笑貌只能成为缅怀。当王平久确认这条消息，并看到8位维和警察的遗体踏上“回家”路的电视画面，他再也坐不住了。他和舒楠不约而同地决定为八位英雄创作一首新歌。<br />
<br />
　　当谭晶拿到这首歌的小样，并听到这一段的时候，已经潸然泪下。身在广州的她当即推掉了所有的活动，决定当晚就录制完成这首歌。成龙也同样改变了自己的行程，全力配合歌曲的录制。成龙、谭晶和这首歌的所有主创者都希望，用这首歌，为八位勇士的“回家”之路送上最后一程。(完）<br />
