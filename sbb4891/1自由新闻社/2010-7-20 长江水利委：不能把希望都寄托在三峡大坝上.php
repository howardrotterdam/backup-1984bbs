																											<div id="postmessage_750750" class="t_msgfont">三峡大坝固若金汤，可以抵挡万年一遇洪水 <br />
<br />
--------------------------------------------------------------------------------<br />
 <br />
<a href="http://www.sina.com.cn" target="_blank">http://www.sina.com.cn</a> 2003年06月01日08:54 金羊网-新快报 <br />
 <br />
　　泄洪坝段曾出现的多条裂缝已全部处理万无一失<br />
<br />
　　新华社记者　施勇峰<br />
<br />
　　今天零时，矗立在长江上的三峡大坝关闭泄水闸门，滚滚江流将在这里止住奔腾的脚步。<br />
<br />
　　三峡大坝是否固若金汤？它能够抵挡来自峡江汹涌激流的冲击吗？<br />
<br />
　　蓄水前，国务院验收组组织几十名专家在大量检查验收的基础上，对大坝进行了最后的鉴定验收。结论是令人满意的：已建成的三峡大坝质量满足了设计要求，大坝蓄水将是安全的。<br />
<br />
　　质量至高无上<br />
<br />
　　三峡大坝全长2.3公里，这座要用1800万立方米混凝土浇筑而成的大坝坝顶海拔高程 <br />
185米，大坝实际浇筑最大高度为181米，有60层楼房那么高。正常蓄水后的三峡水库库容为393亿立方米，坝上坝下有110多米落差。按照设计，三峡大坝必须抵挡万年一遇的长江洪水。如此巨大的落差和库容压力，要求三峡大坝必须具备钢铁般的质量。<br />
<br />
　　在三峡工程工地上，随处可见“千年大计、国运所系”“对国家负责、对人民负责、对子孙后代负责”的大幅标语。在大坝施工中，进度、效益都要服从质量，质量可以否决一切。从2001年1月1日起，所有工程质量事故都被记入承包商的档案，对发生质量事故而又隐瞒不报的，将被取消在三峡的投标资格。<br />
<br />
　　三峡工程建立了非常完善的质量保证体系。每个施工单位都有层层把关的质量自检机构。近千名质量监理分布在施工一线，对混凝土浇筑和隐蔽工程进行铁面无私的旁站监理。作为三峡工程的项目法人，中国长江三峡工程开发总公司还聘请了国外的质量专家担任关键部位的质量总监。<br />
<br />
　　为加强对三峡施工质量的监督把关，国务院三峡工程建设委员会从1999年6月开始，向三峡工程派出了质量专家检查组，不定期深入工地进行质量检查。由国务院直接派出质量检查专家组，这在重点工程中是绝无仅有的。到目前为止，质量检查专家组已经对三峡工程质量进行了9次检查和调研，向国务院提交了4份年度质量检查报告。这些在水利水电界威望极高的“泰斗”、权威，指出质量上存在的问题毫不客气，成为三峡工程最严厉的把关人。<br />
<br />
　　水利“泰斗”张光斗教授，已是90高龄，在对三峡工程的质量检查中，他戴着安全帽，爬到几十米高的三峡大坝导流底孔，用手细细触摸底孔孔面。当他发现泄洪大坝14号导流底孔混凝土浇筑存在蜂窝麻面、汽泡等质量问题时，对设计和施工人员提出了严厉批评。根据张光斗教授和专家组的意见，三峡总公司立即组织有关方面对存在质量问题的部位进行了处理，并举一反三，对导流底孔混凝土浇筑质量进行了一次全面检查和修复。<br />
<br />
　　工程合格率100％<br />
<br />
　　三峡工程从1993年开工至今，已经对13万多个单元工程进行了质量验收。每年的质量验收合格率都是100％。对于一般工程来说，这已是不错了。但在三峡，合格已不是标准，优良率才是考核的标准，经得起历史检验才是三峡大坝质量追求的目标。<br />
<br />
　　关于大坝混凝土浇筑，国家和行业有一系列质量检验标准。但三峡大坝施工，不以达到这些标准为满足。中国长江三峡工程开发总公司参照国家和行业有关质量技术标准，制订了三峡工程《混凝土拌和生产质量控制及检验》等11个质量标准和技术规定。这些标准和规定高于国家和行业质量标准。为严把检验关，三峡工程专门设有测量中心、试验中心、金属结构测量中心和安全监测中心等监测机构，充分运用现代高科技手段对三峡工程质量进行全方位监测。三峡工程试验中心配备有国内或国际先进水平的试验检测设备。中心工作人员依靠先进的技术手段，制订了混凝土骨料、水泥、外加剂等9个方面的质量标准及检测、检验方法。高标准，严要求，使三峡大坝建设质量不断上台阶。1999年三峡工程优良率为80.7％，2000年为81.5％，2001年达到86.2％，2002年提高到91.9％。<br />
