																											<div id="postmessage_749765" class="t_msgfont">这新闻评论网易被关掉了，所以发到这里来了<br />
<br />
核心提示：据长江水利委员会的消息，长江三峡水库预计将会迎来超过1954年和1998年长江上游洪峰的最高值。央视评论员白岩松引述长江水利委主任蔡其华的话说，今年不能把全部的希望都寄托到三峡大坝上，因为想要完成任务，它得达到175米的高程，但是底下还有很多人未移走。<br />
央视7月20日报道 <br />
<br />
《新闻1+1》2010年7月19日完成台本<br />
<br />
――洪水：今年与98年真一样吗？<br />
<br />
主持人（李小萌）：<br />
<br />
欢迎来到《新闻1+1》。<br />
<br />
在今天一天的新闻当中，我想关注度最高的莫过于跟长江流域的洪水有关，而在各种各样的新闻标题当中，最吸引人注意的是长江上游的洪峰可能会超过1998年。1998年的大洪水当时的灾情，当时军民共同抗洪的场面至今仍然历历在目，而12年过去了，相同的大洪水袭来，我们的抵御能力是否发生了改变？<br />
<br />
（播放短片）<br />
<br />
字幕提示：<br />
<br />
7月19日 新闻<br />
<br />
主持人：<br />
<br />
据长江水利委员会的消息，长江三峡水库预计将会迎来超过1954年和1998年长江上游洪峰的最高值。<br />
<br />
解说：<br />
<br />
今天，陕西岚皋县发生山体滑坡，20人下落不明；今天，重庆城口县发生山体滑坡，并形成堰塞湖，上万人安全受到威胁；今天，四川广安渠江发生160年来最大洪峰，整个广安老县城已有三分之二被洪水淹没；今天，特大洪水席卷鄱阳县北东中部，15个乡镇平地起水1至2米；今天，重庆潼南、云南绥江、南京湖口、江西九江、湖北武汉黄梅、安徽砀山，我们已经很难一一梳理全国各地严峻的灾情。而来自国家防总的数据，今年以来各地的洪涝灾害已经造成近亿人口受灾。今天，最让人不安的消息同样来自国家防总，长江上游逾6万立方米每秒的大洪水正式形成，这也是三峡大坝建成以来最大的一次考验。<br />
<br />
此外，防总还通报说，长江上游支流嘉陵江、岷江和沱江发生超警洪水，嘉陵江支流、渠江、汉江支流任河与坝河发生超历史记录特大洪水。长江上游干流发生超保证水位洪水且继续上涨。长江中游主要控制站九江、大通河段仍超过警戒水位，洞庭湖水位与昨天基本持平，鄱阳湖水位稍有回落，淮河上游干流发生超警洪水，太湖水位继续缓涨。<br />
<br />
魏山忠（长江防总办公室主任）：<br />
<br />
预计未来一到两天还有强降雨发生，受降雨影响，三峡入库流量预计将直逼7万（平方米/秒），洪水的峰值将超过历史上的1954年和1998年的（最高峰值）。<br />
<br />
记者：<br />
<br />
大概是哪天？<br />
<br />
魏山忠：<br />
<br />
大概在7月20号晚上将达到这个峰值。<br />
<br />
解说：<br />
<br />
今天，一系列消息显示长江干流将接受洪水的考验。<br />
<br />
魏山忠：<br />
<br />
尽管这次峰值很大，但从洪水的量级上来讲，还远不及1954年和1998年，加上三峡水库的巨大的拦洪作用，长江中下游防洪形势可以得到有效地控制。现在三峡的调度，一个是迎战上游可能发生的大洪水，同时要尽量做到不过多加重中下游的防洪压力。坚持24小时密切地监测预报，滚动地会商，密切地关注整个汛情的进展。<br />
<br />
解说：<br />
<br />
事实上，连续的暴雨已经使长江中下游的江西、湖南、湖北等省面临严峻考验，而面对此次长江上游有可能超过1998年的洪峰，很多人对三峡工程寄予厚望。今天，三峡集团总公司将下泄流量由34000立方米每秒逐步加大到40000立方米每秒。<br />
<br />
蔡其华（长江水利委员会主任）：<br />
<br />
现在按40000立方米每秒控泄，不大。城陵矶超警戒大概0.7米有，沙市不超警戒，武汉江段也不超警戒。如果不是按40000立方米每秒控泄，70000立方米每秒下来，中下游将全线超警。那将使防汛面临严峻形势。<br />
<br />
解说：<br />
<br />
消峰40%，这是今天我们已经看到的长江三峡大坝发挥的作用，而一场比一场猛烈的暴雨，一个又一个超历史记录的洪峰，面对汛情我们希望三峡大坝能在接下来发挥巨大的作用，以减轻中下游的压力。而在长江中下游，事实上目前所发生的险情已经在触动着公众的神经，我们希望不要发生1998年那样的洪水。<br />
<br />
字幕提示：<br />
<br />
1998年6月12日-27日，鄱阳湖水系暴发洪水，抚河、信江、昌江水位先后超过历史最高水位。<br />
<br />
12年后，今年6月21日，江西抚河上游一处大堤突然出现决口。<br />
<br />
1998年7月24日，宜昌洪峰流51700立方米每秒，石首、监利等水文站水位超历史最高水位。<br />
<br />
12年后，今年7月19日，长江水利委员会预报，三峡入库流量预计将直逼7万平方米/秒。<br />
<br />
1998年8月，长江中下游及两湖地区水位居高不下，长江上游接连出现5次洪峰。<br />
<br />
1998年7月和8月，全国共有29个省区市遭受不同程度的洪涝灾害，受灾人口达到2.23亿人，超过3000人死亡。<br />
<br />
2010年8月，我们会面临什么？<br />
<br />
主持人：<br />
<br />
看着这些非常熟悉的画面，让人觉得揪心。岩松，今天第一个问题单刀直入，12年过去了，我们的抵抗能力发生了什么样的改变吗？<br />
<br />
白岩松（评论员）：<br />
<br />
我们不能把希望寄托在不发生像1998年那样的洪水上，因为老天爷的事儿我们还真管不了。但是我们必须寄希望于即使发生跟1998年一样的洪水，甚至超过1998年的洪水，但是由于这12年来我们所累计的进步，我们也不至于那么狼狈，损失也可以降到最少。<br />
<br />
主持人：<br />
<br />
这个希望，这个信心非常足吗？<br />
<br />
白岩松：<br />
<br />
我觉得首先从技术上的层面上来说，虽然明天早上8点钟到三峡库区洪水的峰值将达到7万立方米，超过了1998年，但这只是局部的。这个局部既包括空间，也包括时间，因为总的降雨量1998年更长。另外，总的洪水量也远远不如1998年。因此，在数字上我们好像先踏实了一下，但是别忘了1998年的洪水主要的压力在8月中旬，还真不是现在。但是我更关注另外三点变化，简单地说三个相当于题目吧：<br />
<br />
第一，1998年的时候还有很多的豆腐渣工程，我们水利的设施还欠账很多，但是1998年发起了大江大河这种水利以及堤坝的整治，我们该发挥作用了吧？<br />
<br />
第二，1998年洪水的时候，三峡大江截流刚过了半年的时间，三峡工程还没影儿呢，但是现在三峡工程已经到了175米高程，能不能发挥它蓄洪调沙，尤其蓄洪整个这么重要的作用？<br />
<br />
第三，2003年的“SARS”之后，各地政府都有了相关的处理各种应急事件的预案，也包括洪水来袭。因此，我们可不可以更有序地去防洪，不至于出现人海战术，成千上万人到堤坝的被动局面？<br />
<br />
如果这三点解决好了，即使有超过1998年的洪水，可能我们也不那么狼狈，损失也会很小。<br />
<br />
主持人：<br />
<br />
那在今天，包括这几天的新闻当中，你刚才看到的三点变化，或者说你的三点设想真的在发生作用吗？<br />
<br />
白岩松：<br />
<br />
我刚才说的都是问号，但是从一个新闻人的角度来说，希望它都发挥作用。比如说从堤坝的角度来说，因为从1998年开始的时候，当时1998年洪水一完，我就采访了新任的水利部部长汪恕诚，他跟我讲，国家真的1998年痛定思痛。<br />
<br />
主持人：<br />
<br />
下定了决心？<br />
<br />
白岩松：<br />
<br />
对，国家出钱，大江的堤坝、主干堤坝全是国家出钱，这个会起到很大的作用。而且我相信豆腐渣工程朱总理在九江说完那句话之后，恐怕出现的概率也不会那么高。<br />
<br />
主持人：<br />
<br />
现在我们特别关心三峡大坝。<br />
<br />
白岩松：<br />
<br />
对。<br />
<br />
主持人：<br />
<br />
你觉得在今年洪峰来的时候，三峡大坝能不能发挥设计的时候人们期待的那样的作用？<br />
<br />
白岩松：<br />
<br />
既有好消息，也有不好的消息。好消，想想明天早上8点钟7万立方米来到，但是三峡送到中下游的时候才是4万立方米，起码它把3万立方米留在了自己的库区里，而现在还不到150米的高程，还有一定相当大的余地。因此从某种角度来说，已经发挥了三峡在面对洪水时一定调峰的作用。<br />
<br />
但是为什么我说还有不好的消息呢？因为现在，按理说我们到了175米高程，可以抵御百年洪水，但是现在的三峡工程想抵御百年洪水是几乎不可能的，为什么？在175米高程之下还有2.847万的移民没移走。因此今年的三峡水库想要达到最高的调解洪水的能力，基本上还做不到。因此，有关的负责人也说，今年三峡库区面对洪水的能力也是有限的。<br />
<br />
主持人：<br />
<br />
究竟是不是有限的，有多有限？我们下面要做一个电话连线，我们来连线三峡枢纽建设运行局防洪管理处的王海处长，王处长，您好。<br />
<br />
王海（三峡枢纽建设运行局防洪管理处处长）：<br />
<br />
您好。<br />
<br />
主持人：<br />
<br />
你觉得现有的库容量能不能迎接即将到来的洪峰？<br />
<br />
王海：<br />
<br />
三峡现在已经具有正常运行期221.5亿立方米防洪库容的能力。应该说剩下的库容主要是留着应对以后更大的洪水，并不是说一次洪水之后就用完的，因为后期可能还会有大的洪水。作为三峡枢纽来说，本身是具有221.5亿的防洪库容，作为枢纽本身应该是安全的。<br />
<br />
主持人：<br />
<br />
您讲的是它的一个安全性，现在可能很多公众非常关心，针对今年即将到来的大洪水，三峡库区能否起到蓄洪、泄洪设计上的作用？<br />
<br />
王海：<br />
<br />
应该来说三峡今年是具备有正常运行期设计的防洪能力，这是肯定的。<br />
<br />
主持人：<br />
<br />
好，我们也拭目以待。<br />
<br />
其实像三峡的洪水，长江的洪水三峡库区之外，在中下游很多的湖泊也应该起到非常重要蓄洪的作用，但是我们依然在新闻当中看到了这些湖泊的堤坝告急的消息。<br />
<br />
（播放短片）<br />
<br />
杨松涛（本台记者）：<br />
<br />
这里是江西省九江市彭泽县的棉船镇，近期由于长江水位持续升高，导致棉船镇的这条堤坝发生了东岸渗漏和泡泉。<br />
<br />
徐文东（南京军区某部工兵营营长）：<br />
<br />
当时水已经漫过围堤了，险情比较严重。<br />
<br />
解说：<br />
<br />
昨天位于长江中心的九江县江洲镇、彭泽县棉船镇江岸相继出现崩岸险情。长江棉船段水位已超警戒1.4米，在棉船镇金星村九组的两处崩岸，险段长度已经超过200米，而保护人口3.7万的江州大堤，水位也已超警戒线1.1米。事实上，江西九江的棉船大堤在1998年的大洪水时发生过三次崩岸，当时的崩岸造成了大堤外的防护林带向里收缩了100多米，而江州州头段的堤坝在当时决口300米。近年来，这段堤坝一直属于抗洪能力的薄弱区。<br />
<br />
杨松涛：<br />
<br />
今天我们又到了棉船镇，到了昨天我们去采访崩岸的现场，经过南京军区某部工兵营的官兵昨天一整天的抢险，现在崩岸现场可以说是险情基本排除。今天我们到达这里的时候，这些官兵正在进行另外一项工作，正在进行抛石护堤。<br />
<br />
解说：<br />
<br />
很多人不会忘记12年前的1998年，那个夏季洪水成了最重要的记忆。29个省市自治区受灾，500万所房屋倒塌，2000万公顷土地被淹，经济损失1600余亿。事实上，对于1998年的那次洪水，事后就有水利专家分析说，除了一些大堤大坝的工程质量之外，分蓄洪量的大量减小和湖泊调蓄能力的降低，也是主要原因。<br />
<br />
今天，面对长江上游有可能超1998年的洪峰，我们最大的变化或许就是三峡工程全部峻工。从大江截流到首批机组开始发电，再到全部机组投入运行，整个工程的投资不少于1800亿，而它的防洪功能显然是排在第一位的。此外，1998年大洪水之后，国家连续投入300多亿，重点加固长江中下游堤防。然而12年后，对于分蓄洪量减少和湖泊储水能力降低的现实，却依然让人忧心忡忡。<br />
<br />
陈建湘（城陵矶水文站勘测队副队长）：<br />
<br />
主要隐患是防止出现管涌或者滑坡，超过32.50米的警戒水位，湖南省要有几万人员上堤巡查。1998年出现历史最高水位35.94米，也就是到这个位置。<br />
<br />
记者：<br />
<br />
1998年的时候曾经到这儿？<br />
<br />
陈建湘：<br />
<br />
对。离现在睡眠还有3.5米的高度。<br />
<br />
解说：<br />
<br />
今天，超过警戒水位的洞庭湖已经启动防汛三级预案。事实上，洞庭湖防汛是长江流域最为敏感和重要的一个区域，而去年年底的一份调查显示，目前洞庭湖储水量只有7.7亿立方米，不到分水器正常水量的十分之一。<br />
<br />
同期：<br />
<br />
开发区内一个排涝通道银湖却正在大规模施工，10亩湖面被填埋。<br />
<br />
山西运城的苦池水库当中，却建设了一排别墅和数百米长的商业街。<br />
<br />
解说：<br />
<br />
今年6月，一份武汉市水务局的调查数据就显示，50年来，近100个湖泊人间蒸发。面对这样的现状，湖泊与水资源环境研究领域权威专家金伯欣认为，武汉湖泊数量减少、面积锐减，既有特殊历史背景下围湖造地、围湖养鱼的“历史之殇”，也有因城市建设需要而填湖占湖的“发展之殇”，更有屡禁不止的违法填湖的“现实之殇”。而在全国，自20世纪50年代以来，全国大于10平方公里的湖泊中，萎缩减少面积9570平方公里，共减少蓄水量516亿立方米。“填排涝湖，造高尔夫”、“水库边上造别墅”，媒体的大量报道透露出的是公众对于防汛现状的一种担心。<br />
<br />
7月15日以来，连续的强降雨已经使江西、湖南、湖北等长江中下游地区纷纷告急，面对不断出现的泥石流、山洪、城市内涝、一些堤坝决口的新闻，使我们对今天长江上游出现98洪峰的新闻显得格外敏感，防患于未然，我们准备好了吗？<br />
<br />
主持人：<br />
<br />
现在我们要来电话连线中国水利水电科学研究院的严登华教授，严教授，您好。<br />
<br />
严登华（中国水利水电科学研究院教授）：<br />
<br />
主持人，您好。<br />
<br />
主持人：<br />
<br />
我们看到了长江中下游的一些支流，包括一些本来应该有蓄洪能力的湖泊也在频频地告急，除了降雨量大的自然因素之外，还有什么样的原因？<br />
<br />
严登华：<br />
<br />
从工程的角度来说，因为长江中下游中小型防洪的水利工程都是在50年代、60年代、70年代新建的，当时的技术水平、规划、设计和施工的水平跟现在相比来说，都有很大的差距。<br />
<br />
另外一个方面，这些工程都运行了几十年，虽然我们最近一些年来都非常重视这些工程的除险加固工作，但是因为数量非常大，很难一步到位。<br />
<br />
第三个方面的原因，这些工程缺乏一些有效的配套措施，包括通讯和预警预报的设施都比较弱一些。<br />
<br />
还有一个方面，整个的管理水平、调度能力以及管理人员整体的专业水准方面都有待于进一步提高。<br />
<br />
主持人：<br />
<br />
能够做到除险加固的能够占到百分之多少呢？<br />
<br />
严登华：<br />
<br />
目前还没有一个非常准确的数字，但是相比于前几年来说，这个水平得到了很大的提高。<br />
<br />
主持人：<br />
<br />
是否在今年这样一个降水和洪水的面前真的发挥了作用？<br />
<br />
严登华：<br />
<br />
应该说发挥了它很积极的作用。<br />
<br />
主持人：<br />
<br />
可是我们看到告急的消息也是不少。<br />
<br />
严登华：<br />
<br />
是，因为这些险工险段、病险的这些水利工程数量还是非常多的，前期一些历史的欠账在今年全部暴露出来了。<br />
<br />
主持人：<br />
<br />
好，谢谢严教授提供的观点。<br />
<br />
岩松，其实严教授讲的是一个历史欠账问题，其实今年看到的这个灾情有没有新的隐患存在？<br />
<br />
白岩松：<br />
<br />
我刚才说了，从1998年开始的时候国家痛定思痛出钱，对主干防洪的堤坝进行了连续的修建。这一点我觉得担心的程度要减少了，但是别忘了支流的一些堤坝是由地方出钱，在地方出钱的时候资金跟不跟得上，工程的质量怎么样，这是一个要让你必须感到担心的地方。所以会看到今年很多并不是在长江主干堤会出现很多的问题，反而是到了支流或是怎么样，这是今年跟1998年不一样的一种隐患。<br />
<br />
还有在这十几年快速发展的过程中，人抢地其实也在抢自己的命，为什么这么说呢？其实湖泊的存在是一种天然的水库，它会对有洪水或者大水的时候，会起到自然平抑的作用。但现在把人家库容的量、面积越减越少，是盖了别墅、盖了房子，又开始占地了等等，好像表面得利益了，但关键时刻这东西要你命啊。所以我觉得但愿不出问题。<br />
<br />
主持人：<br />
<br />
像这样的隐患，你觉得它还有可逆的可能性吗？<br />
<br />
白岩松：<br />
<br />
我觉得这个东西就是看你怎么严格地去执行我们相关的法规。因为每次总是在遇到灾情的时候才突然意识到不看重的东西原来这么重要。在风和日丽的时候，没人看重这些事情，但是这个时候就要看重了。我觉得国家也应该像查很多的事情一样，像一个红线死保土地，也要保证湖泊，因为不能总是依靠建一个又一个三峡大坝，所以在这个时候我们看着急剧衰微的一个又一个湖泊的时候，你会感到很担心。但是面对今年，首先当然不希望洪水很快能够过去，不希望发生1998年那么大的洪水，但是同时我们也必须有另外的一种相信，这12年的进步，我们应该对假如今年发生了跟1998年一样的洪水，甚至更大的时候，我们也可以保有谨慎的乐观。<br />
<br />
主持人：<br />
<br />
谨慎的乐观？<br />
<br />
白岩松：<br />
<br />
对。<br />
<br />
主持人：<br />
<br />
前面你也提到了这些年各地都在建立一个应急预案。<br />
<br />
白岩松：<br />
<br />
对。<br />
<br />
主持人：<br />
<br />
这个应急预案的启动，在今年的抗洪当中发挥的作用你看到的有什么？<br />
<br />
白岩松：<br />
<br />
首先我们回忆1998年洪水的时候，要知道相当多靠的是人拉肩扛，甚至士兵堵，成千上万人上堤坝，所以谱写了一个可歌可泣的抗洪精神。但是总有精神是不行的，因为它对人的压力简直太大了。所以有两点，长江水利委的主任蔡其华，一是她说到了，今年不能把全部的希望都寄托到三峡大坝上，因为想要完成任务，它得达到175米的高程，但是底下还有很多人没移走。另一个就是不能走人海战术，不能被动。：</div>

							
							
							
							
