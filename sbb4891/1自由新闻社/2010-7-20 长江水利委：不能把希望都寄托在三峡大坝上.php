<br />
<br />
一、三峡水库概况 <br />
<br />
<br />
新华网上拷来的，如果是骗子提供的假情报，那也不是我的责任。 <br />
<br />
三峡水库大坝在宜昌市三斗坪。在正常蓄水位１７５米、５年一遇来水的回水线时，水库末端位于重庆市江津市羊角滩（芦注：根据Google地图在寸滩西南约40公里处），水库长度为６６２. ９公里、水面面积为１０４５平方公里。水库将淹没６３２平方公里陆地，涉及重庆、湖北两省的１９个县市区，淹没线以下人口８４. ６２万人。考虑到人口增长因素，规划动迁人口共计约为１２０万人。是全世界淹没面积最大的水库。 <br />
<br />
三峡水库的平均宽度为１５７６米，没有一个“大肚子”库段，是一个典型的狭谷型水库，又称河道型水库，不像北京密云水库、浙江新安江水库、湖北丹江口水库那样为典型的湖泊型水库。 <br />
<br />
<br />
二、斜率计算 <br />
<br />
<br />
从以上简介立刻可以看出，这背时水库其实是一条河床容量加大了的河。只有绝对而非相对的白痴，才会认定河面的斜率为零。而wm昨天转来的那个作为证明王维洛是骗子的帖就坚决主张这白痴观点。 <br />
<br />
鉴于： <br />
<br />
1）该水库没有“大肚子”库段，而是一个狭窄的水槽，因此，大坝与水槽大致等宽，不必考虑斜率对两侧的分散（dch提出的问题可以忽略不计，我原来是当成大肚子湖泊型水库考虑了）； <br />
<br />
2）除非处于洪水期与枯水季节，水库的流量应该与原有河道的相等，因此对水面斜率的影响可以不计； <br />
<br />
若以K代表水库水面斜率，以k代表原来河道的斜率，以H代表从羊角滩到三斗坪的原来河道的容量，以V代表库容，则计算水库水面斜率的公式应该是： <br />
<br />
K = k H / V <br />
<br />
若水库不蓄水，则H=V，亦即库容等于原来河道的容量，此时水面的斜率就是原有河流的斜率。若V大于原来河道容量，则水库的水面斜率小于原来河面斜率。库容越大则斜率越小，当V 趋近于无穷大时，水库水面的斜率才会趋近于零，此乃极限状况，只有白痴才会以为高峡出平湖。 <br />
<br />
这儿说的“水库水面的斜率”是指平均值，它并不是个常数，而是呈S形曲线，亦即在库头库尾巴斜率比较大，而中间一段比较平缓。这从wm转贴的实测数据中可以看出：从巴东到忠县近300公里也才有一米多的落差，而从忠县到库尾寸滩的落差就竟然有30米左右。 <br />
<br />
<br />
三、该公式的涵义 <br />
<br />
<br />
从该公式可以导出，大坝水位越高，则斜率越低，反之，大坝水位越低，则斜率也就越高，这与实测数据变化相符。根据wm转来的那个工程师日记： <br />
<br />
巴东水位为107.11米时，寸滩水位为162.80米，落差为55.69米 <br />
<br />
但巴东水位升到134.38米时，寸滩水位仅升到166.20米，落差为31.82米 <br />
<br />
由此可见，下游水位越高，则斜率越低。这儿的变化并非线性关系，倒像是指数曲线。当巴东达到175米时，落差可能在5-10米左右（只是毛估，并未计算），换言之，寸滩的水位可能达到180-185米，但仍然超过了王维洛披露的移民线。 <br />
<br />
<br />
四、结论 <br />
<br />
<br />
A、王维洛的警告可能成立。 <br />
<br />
B、简单思索即可发现那群水文权威完全是令人瞠目结舌的白痴，居然会将一个车厢形的水库看成是平湖，认定斜率为零。 <br />
<br />
C、MW提供的数据非但没有证明王维洛是骗子，反倒证明他是双料白痴，第一料是他竟然会认同斜率为零的超级白痴疯话，第二料是他专门引用材料反驳自己。这已经是第二次了，头次他贴出中缅边境线图来证明我造谣，恰好证明他诬陷。这次他转贴实测数据证明王维洛是骗子，恰好证明他自己才是蓄意贪污关键数据弄虚作假的骗子。 <br />
