																											<div id="postmessage_749792" class="t_msgfont">北京市民任星辉向财政部申请公开电费中所含三峡建设基金收支情况遭拒，昨天将财政部诉至一中院。一中院正在审查是否符合受理条件。<br />
<br />
　　任星辉说，为了筹集三峡工程建设资金，上世纪90年代初，财政部等部门联合发出通知，决定除西藏等地方外，每度电征收3厘钱用于三峡建设，后增加到4厘钱，后来在一些发达地区提高到7厘钱，“不光我，北京居民交的电费中都包括这个钱。”<br />
<br />
　　去年10月，任星辉向财政部提交了公开三峡工程建设资金相关政府信息的申请。财政部答复中称，2008年份的收支已公开，至于其他信息，因与任星辉本人的生产、生活、科研等特殊需要无直接关联，不予提供。任星辉向财政部申请行政复议，财政部作出不予受理决定。<br />
<br />
