1、水库防洪防旱的机制何在？ <br />
芦笛 @ 2009-4-10 1:08 阅读(103) 评论(0) 推荐值(0) 引用通告 分类: 未归类 <br />
再大慈大悲为“老资格过来人本专业”的wm疗次愚。这是今日最后一帖，过后我就要睡觉去了，讲的都是人类常识。 <br />
<br />
人类修水库，无非是用它来稳定河道一年四季的供水量，如同电工学中使用电容器稳压，使用电感线圈稳流一般，这就叫“缓冲”。 <br />
<br />
在枯水季节，水流供应太少，人类自然希望供水量能多些；在洪水季节，水流供应又太多，人类自然也就希望供水量能减少些。解决这个问题的办法，就是在河道的中上游修水库，以此来调节河流的流量，使之一年内的流量差别不要太大。水库容积越大，则调节能力越强。其工作机制如下： <br />
<br />
枯水季节：流入小于流出――下游流量大于上游流量――库内水位下降 <br />
<br />
洪水季节：流入大于流出――下游流量小于上游流量――库内水位上升 <br />
<br />
任何一个非白痴都能立即明白：水库在此相当于一个串联在江河里的电感线圈，起到稳流作用，区别只在于电流是回路中处处相等，而水库只能调节下游的流量，枯水时它释放出洪水期间存储下来的积水，增加下游流量，而洪水期间它储入上游来的过多的水，使得下游的水量不至于太大。因此，库内水位的升降其实与河道一致：枯水季节下降，洪水季节上升。 <br />
<br />
以上所说，当然只能在一定的限度内。如果枯水太严重，水库容积又有限，则枯水季节靠放水增加下游流量也就有限，如果遇到大旱，河床干涸，则水库也照样可能见底。反过来，如果洪水太大，水库水位上升到了危险程度，为了预防垮坝，就不得不大量排水，流出量可能大于流入量，则此时水库非但没有防洪的功能，反倒加重了下游的水患，比没修水库还糟糕――若没有水库，则上游下游的流量一致，并不会发生下游的流量在洪水期间比上游还大的雪上加霜效应。 <br />
<br />
如果一旦垮坝，则水库的危害就更大。尤其是发生连锁反应就更如此：上游的水库垮坝，释放了存储的所有积水，使得下游突然出现巨大洪峰，又冲毁了下游的水库，再释放出该水库内的积水，使得下游的洪峰更加巨大。1975年河南大水灾就是这么造成的。据《腾讯新闻》报道： <br />
<br />
“1975年8月，在河南省驻马店等地区、1万多平方公里的土地上，共计60多个水库相继发生垮坝溃决，近60亿立方米的洪水肆意横流。1015万人 受灾，超过2.6万人死难，倒塌房屋524万间，冲走耕畜30万头。纵贯中国南北的京广线被冲毁102公里，中断行车16天，影响运输46天，直接经济损 失近百亿元，成为世界最大最惨烈的水库垮坝惨剧。” <br />
<br />
不难想象，如果没修那些烂水库，则这人间惨剧也就不会以如此可怕的规模发生了。因此，只有白痴才会以为只要修水库就能防洪，而要修水库就修得越大越好。水库当然有防洪作用，但上面已经说过了，那只能在一定限度内工作。 <br />
<br />
明白了这些最起码的人类常识，则立刻能看出WM是何等的SB与白痴（说明，这两个用语都是他率先使用的，他骂王维洛是SB，骂我是老年性白痴，有楼下帖子为证），竟然说出这种胡话来： <br />
<br />
“来洪水之前，水库当然要放水呀，如果洪水来之前，水库都是满满的，唉，不说了。芦笛是错误理解‘正常蓄水位’，什么叫正常，不发大水才是正常，发大水就是不正常。” <br />
<br />
这就是说，上面说的人类常识不成立，库内水位不是旱季下降而雨季上升，跟一般河道一样，而是反过来，旱季上升到达“正常水位”，而雨季到来则赶快放水！ <br />
<br />
他还怕大家不理解，进一步解释： <br />
<br />
“５年一遇来水不是五年一遇的洪水，因为蓄水的时候都是汛期的末期，基本上不会发洪水，来水是只在这个时期，比如，每年10月，11月期间五年一遇的上游来水，来确实回水线。明白吗？” <br />
<br />
