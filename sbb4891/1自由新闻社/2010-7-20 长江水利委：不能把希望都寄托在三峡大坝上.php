　　消峰40%，这是今天我们已经看到的长江三峡大坝发挥的作用，而一场比一场猛烈的暴雨，一个又一个超历史记录的洪峰，面对汛情我们希望三峡大坝能在接下来发挥巨大的作用，以减轻中下游的压力。而在长江中下游，事实上目前所发生的险情已经在触动着公众的神经，我们希望不要发生1998年那样的洪水。<br />
<br />
　　字幕提示：<br />
<br />
　　1998年6月12日-27日，鄱阳湖水系暴发洪水，抚河、信江、昌江水位先后超过历史最高水位。<br />
<br />
　　12年后，今年6月21日，江西抚河上游一处大堤突然出现决口。<br />
<br />
　　1998年7月24日，宜昌洪峰流51700立方米每秒，石首、监利等水文站水位超历史最高水位。<br />
<br />
　　12年后，今年7月19日，长江水利委员会预报，三峡入库流量预计将直逼7万平方米/秒。<br />
<br />
　　1998年8月，长江中下游及两湖地区水位居高不下，长江上游接连出现5次洪峰。<br />
<br />
　　1998年7月和8月，全国共有29个省区市遭受不同程度的洪涝灾害，受灾人口达到2.23亿人，超过3000人死亡。<br />
<br />
　　2010年8月，我们会面临什么？<br />
<br />
　　主持人：<br />
<br />
　　看着这些非常熟悉的画面，让人觉得揪心。岩松，今天第一个问题单刀直入，12年过去了，我们的抵抗能力发生了什么样的改变吗？<br />
<br />
　　白岩松（评论员）：<br />
<br />
　　我们不能把希望寄托在不发生像1998年那样的洪水上，因为老天爷的事儿我们还真管不了。但是我们必须寄希望于即使发生跟1998年一样的洪水，甚至超过1998年的洪水，但是由于这12年来我们所累计的进步，我们也不至于那么狼狈，损失也可以降到最少。<br />
<br />
　　主持人：<br />
<br />
　　这个希望，这个信心非常足吗？<br />
<br />
　　白岩松：<br />
<br />
　　我觉得首先从技术上的层面上来说，虽然明天早上8点钟到三峡库区洪水的峰值将达到7万立方米，超过了1998年，但这只是局部的。这个局部既包括空间，也包括时间，因为总的降雨量1998年更长。另外，总的洪水量也远远不如1998年。因此，在数字上我们好像先踏实了一下，但是别忘了1998年的洪水主要的压力在8月中旬，还真不是现在。但是我更关注另外三点变化，简单地说三个相当于题目吧：<br />
<br />
　　第一，1998年的时候还有很多的豆腐渣工程，我们水利的设施还欠账很多，但是1998年发起了大江大河这种水利以及堤坝的整治，我们该发挥作用了吧？<br />
<br />
　　第二，1998年洪水的时候，三峡大江截流刚过了半年的时间，三峡工程还没影儿呢，但是现在三峡工程已经到了175米高程，能不能发挥它蓄洪调沙，尤其蓄洪这么重要的作用？<br />
<br />
　　第三，2003年的“SARS”之后，各地政府都有了相关的处理各种应急事件的预案，也包括洪水来袭。因此，我们可不可以更有序地去防洪，不至于出现人海战术，成千上万人到堤坝的被动局面？<br />
<br />
　　如果这三点解决好了，即使有超过1998年的洪水，可能我们也不那么狼狈，损失也会很小。<br />
<br />
　　主持人：<br />
<br />
　　那在今天，包括这几天的新闻当中，你刚才看到的三点变化，或者说你的三点设想真的在发生作用吗？<br />
<br />
　　白岩松：<br />
<br />
　　我刚才说的都是问号，但是从一个新闻人的角度来说，希望它都发挥作用。比如说从堤坝的角度来说，因为从1998年开始的时候，当时1998年洪水一完，我就采访了新任的水利部部长汪恕诚，他跟我讲，国家真的1998年痛定思痛。<br />
<br />
　　主持人：<br />
<br />
　　下定了决心？<br />
<br />
　　白岩松：<br />
<br />
　　对，国家出钱，大江的堤坝、主干堤坝全是国家出钱，这个会起到很大的作用。而且我相信豆腐渣工程朱总理在九江说完那句话之后，恐怕出现的概率也不会那么高。<br />
<br />
&nbsp; &nbsp; 深度策划：<br />
<br />
&nbsp; &nbsp;“千年一遇”难掩推责的混乱逻辑：回顾一下近年来，我们好多地方在应对自然灾害和极端气候时，除了救灾，说的最多的就是“我们遭遇了N年不遇的（大旱）或者（洪水）或者（冰雪）或者（严寒）或者（地震）”；往年看汛期新闻，最常见的也就十年、二十年甚或五十年一遇，百年一遇已属罕见。今年“百年”已成小儿科，先是有南方某地发生五百年一遇的特大洪涝，紧接着，青海又出现格尔木河流量超“两千年一遇”的洪水标准，随着越来越多的“N年不遇”接连不断的出现，我们不禁要问，真的有这么多“N年不遇”吗？<br />
<br />
&nbsp; &nbsp; 不用鼓吹能抗多少年一遇，关键是将水利隐患消除：就拿城市防暴雨来说，不要仅仅满足于问题的“手到病除”，而应该追根寻源，该扩大修整下水道的就一次性修好，应该疏通的就经常保持疏通。当然，像西南地区今年的大旱，除了说是“N年不遇”之外，是不是还要检查检查我们的抗旱设备、水利设施，实际上是多年来形同虚设，似有似无，要不然，在大旱面前怎么会无能为力。<br />
<br />
&nbsp; &nbsp; 最重要的还是要防患于未然：可以把责任推给老天爷就算是遭遇了百年不遇的强降雨，如果山上的植被保存得很好，也不一定会发生严重的泥石流；如果城市排水系统设计得好，就不会发生几十条人命在城市里被洪水吞没的悲剧；如果自上而下的防灾应急预案能真正做到科学、缜密、管用，并真正落到实处，就不会出现一场台风、一场暴雨就能卷走几十条、上百条人命…[详细]</div>

							
							
							
							
