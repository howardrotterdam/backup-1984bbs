													<h2>中国新闻审查案例：北京网评会批评网站娱乐化报道罗京病逝</h2>
						
						
																											<div id="postmessage_124372" class="t_msgfont"><a href="http://news.163.com/09/0616/17/5BURMD2E0001124J.html" target="_blank">http://news.163.com/09/0616/17/5BURMD2E0001124J.html</a><br />
北京网评会批评某些网站娱乐化报道罗京病逝事件<br />
2009-06-16 17:45:30　来源: 人民网(北京)<br />
<br />
人民网北京6月16日电 2009年6月16日下午，北京网络新闻信息评议会召开本年度第二次会议，对某些网站报道中央电视台著名播音员罗京同志病逝事件中表现出的娱乐化问题进行评议。<br />
<br />
评议会指出，罗京同志是一位深受亿万电视观众喜爱的优秀电视新闻播音员，他沉稳自信、大气的播音风格感染了改革开放后的一代中国人，给人们留下了难以磨灭的记忆。罗京同志因病逝世后，中共中央政治局常委李长春专门向中央电视台发去唁电，对罗京的26年播音活动给予高度评价，称他：“政治坚定，德艺双馨”。<br />
<br />
对于罗京的不幸早逝以及广大群众的惋惜心情，各类媒体都给予了高度关注，互联网媒体借助信息量大、表现形式丰富的优势更是把此事作为报道重点。从罗京6月5日病逝后的一周内，北京各类网站通过多种形式在显著位置进行报道，并开放网站互动功能让广大网友表达哀悼之情，总体上较好地把握了这一令人悲痛事件的报道方向和尺度。然而在6月11日罗京同志遗体告别仪式上，一些网站在现场的拍摄活动及事后图片报道中出现的娱乐化倾向，则违背了职业操守和报道准则，必须予以批评，提出警告。<br />
<br />
评议会指出，为满足广大电视观众沉痛悼念罗京同志、希望为他送行的要求，罗京同志的遗体告别仪式特别向公众开放，因此一些门户网站娱乐频道派出采编人员到现场进行报道。这些采编人员手持带有“娱乐频道”字样的话筒采访前来送行的群众、罗京的同事、甚至是罗京亲属，并在随后刊发的照片上打上明显的“娱乐频道”标识，在刊发的快讯中，使用“娱乐大视野”的题头，在快讯下刊发画面极不适宜的广告，有的照片中出现面带笑容等极不严肃的人物形象，更有甚者，在罗京亲属的照片上也打满“娱乐频道”的水印。遗体告别仪式本应是肃穆、庄重的，但网站娱乐频道为了突出自己的报道身份，推广频道形象，使用以上种种出格的做法，不仅破坏了现场凝重的气氛，是对死者尊严的不敬，更是对死者亲属和广大观众网友感情的严重伤害。这一不良报道倾向出现后，立刻引起网友的强烈不满和愤怒，一些网友在跟帖、论坛帖文和博客文章中质问：难道丧事也可以被娱乐吗？<br />
<br />
评议会指出，不少国家和地区的媒体组织，均对从业人员和媒体的职业操守和报道准则有明确具体的规定。尤其是对一些特定事件的报道，特别强调要顾及事件相关当事人的感受，要考虑报道内容对说明整个新闻事件是否必要，要考虑报道手法的社会影响。在罗京同志遗体告别仪式上，一些网站娱乐频道的不当做法和不良报道倾向，必须引起业界高度关注和警惕，通过对本次评议内容的举一反三，使业界的职业操守和专业素质不断提升，使报道的准则和手法不断得到规范。<br />
<br />
为此，本评议会特提出以下三项具体要求：一、当事网站应尽快组织娱乐频道所有从业人员进行一次职业道德和报道准则的学习，对此次暴露出来的问题进行认真反思；二、当事网站应对图片登载的准则进行补充和完善，从机制上保证在今后的报道中不再发生类似情况；三、当事网站应在本次评议会后7日内将有关整改的情况书面提交新闻评议会。<br />
<br />
北京网络新闻信息评议会介绍<br />
<br />
2006年4月，由北京市互联网宣传管理办公室指导，北京网络媒体协会主办的北京网络新闻信息评议会成立，对北京网络媒体行业开展新闻信息服务的情况实施社会公众评议。<br />
<br />
相对于法律约束和行政监管的“他律”，社会公众评议属于“自律”范畴。通过建立行业评议会，运用社会公众评议方式实施“自律”的模式源自西方，在我国网络媒体中尚属新生事物，尤其对长期以来重公约、轻执行的我国行业自律现状而言，更具有前瞻性和突破性。<br />
<br />
评议会的架构<br />
<br />
2006年4月3日，中共中央政治局常委李长春同志在京考察，发出了文明办网、文明上网的重要指示。4月9日，北京千龙网等14家网络媒体联合发出文明办网、文明上网倡议，4月13日，作为推进这项活动的重要举措，北京网络新闻信息评议会（以下简称评议会）应运而生。<br />
<br />
1．评议会的宗旨及职能。<br />
<br />
成立该评议会的宗旨是要通过加强自身监督与评议，促进全行业营造积极、健康、向上的网络文化环境。主要职能是依据国家相关法律法规及其它相关行业规约，对涉及北京网络媒体互联网新闻信息服务、新闻职业道德和因新闻侵权问题而引发的新闻纠纷进行评议，并监督评议结果的执行。<br />
<br />
2．评议会的架构与操作。<br />
<br />
评议会设主任1名、副主任2名，主任原则上从北京网络媒体协会成员中产生，一名副主任从网民中产生。同时，为确保评议会的公正，明确规定了主任、副主任须由非任职网络媒体业界人士担任，并任期2年。<br />
<br />
评议会成员总数150人左右，其中政府管理部门代表约20人，行业代表约20人，专家学者约30人，网民代表约80人。同时，对评议员应有的教育水平、年龄、网龄、职业分工、互联网应用水平、分析能力和表达能力都有明确界定。<br />
<br />
评议会成员的产生途径有三种：一是参加评议的成员均可申请派代表参加评议会；二是北京网络媒体协会邀请业界代表、专家学者参加；三是通过媒体等公开渠道邀请各界人士参加。<br />
<br />
评议会下设秘书处，负责受理投诉、例会组织、监督决议执行等事务。评议内容一是来自秘书处受理的举报人来访投诉；二是来自北京网络媒体协会设立的举报热线（网站、举报电话、举报邮箱、信件投诉）；三是来自成员单位就其他成员单位违反公约行为的举报；四是相关政府管理部门针对成员单位的违法、违规行为提出的评议请求。<br />
<br />
评议会不定期召开，每次会议成员由秘书处从评议会成员库中按比例随机选择9名参加，评议内容由9位成员根据秘书处收集的投诉情况进行选择，评议会成员不参加对涉及本单位投诉的评议，一般性评议结果经表决，获多数即可通过，重大评议结果需经2/3多数通过。<br />
<br />
秘书处将评议公报发送各成员单位并监督有关奖励、处罚决定的实施。各成员单位可就涉及本网站的评议结论进行申辩，申请复议，复议由评议会另组人员参与。同时，评议会还要将评议结果反馈投诉者，并进一步征求意见。有关评议结果，须在北京网络媒体协会网站或通过其他方式向社会公布。<br />
<br />
