<br />
2009年春节城区环境宣传方案<br />
<br />
 <br />
<br />
春节是中华民族的传统佳节，是万家团圆的日子。为营造欢乐、喜庆、祥和的节日氛围，现就2009年春节城区环境宣传制定方案如下：<br />
<br />
一、悬挂罗马旗<br />
<br />
悬挂要求：罗马旗为全封闭，左右各一，尺寸为235cm×69cm，整体框架用2cm×2cm方管焊接，刷防锈漆，并用彩钢板折边包角；横杆为3cm×3cm方钢管，长170cm，上下各一根，刷防锈漆；横杆两头不锈钢球直径为6cm。悬挂高度为罗马旗底边离地面2.5米，标语、式样见小样，四种不同内容的罗马旗依次悬挂，均匀分布，各责任单位可兼顾企业自身形象宣传的需要，制作不超过30%的商业性广告穿插悬挂。<br />
<br />
地段及责任单位：姜堰大道（法院至正太大桥）、人民路（皮防所至汽车总站）、步行街（时代超市至烟草公司）、罗塘路（农村合作银行至金湖湾大桥）道路两侧的灯杆上悬挂罗马旗，由农村合作银行负责落实；<br />
<br />
北大街、古田路道路两侧，南大街道路一侧的灯杆上悬挂罗马旗，由交通银行负责落实；<br />
<br />
振兴路（二附中至励才中学）两侧的灯杆上悬挂罗马旗，由建设银行负责落实；<br />
<br />
姜堰大道开发区段（华东五金城至正太大桥）两侧的灯杆上悬挂罗马旗，由开发区负责落实。<br />
<br />
二、制作户外广告<br />
<br />
黄河大酒店对面制作一块户外广告，内容为宣传标语2，字为红色，背景为姜堰美景，由建设局负责落实。<br />
<br />
三、悬挂灯笼<br />
<br />
城区所有单位在大门前悬挂4只灯笼，尺寸统一为120公分高，上贴“欢度春节”字样，字为黄色，由各单位分别负责落实，学校、医院和饭店、商场和超市的环境布置分别由教育局、卫生局、贸易局督查落实到位。各单位不再悬挂新的横（条）幅，已悬挂的横（条）幅请于1月16日前自行清理干净。<br />
<br />
四、灯笼一条街<br />
<br />
步行街灯杆、行人休息亭等处悬挂灯笼，由农行负责落实。<br />
<br />
五、播放宣传标语<br />
<br />
广电局利用电视满屏字幕播放宣传标语；<br />
<br />
财政局利用步行街财政局大楼前过街电子显示屏播放宣传标语；<br />
<br />
社保局利用职业介绍所电子显示屏播放宣传标语；<br />
<br />
建设局利用新世纪广场大屏幕电视播放宣传标语；<br />
<br />
建管局利用锦宸大厦大屏幕电视播放宣传标语；<br />
<br />
纪委利用宁靖盐高速立交桥东侧电子显示屏播放宣传标语；<br />
<br />
政法委利用门前电子显示屏播放宣传标语；<br />
<br />
法院利用门前电子显示屏播放宣传标语；<br />
<br />
人行利用各商业银行网点门前电子显示屏播放宣传标语。<br />
<br />
六、建设局负责于1月16日前对城区相关路段灯杆进行清理。户外广告小样请于1月16日前送宣传部宣传科审定。<br />
<br />
七、所有环境宣传从1月21日开始，到2月12日结束。<br />
<br />
八、各社区环境宣传由姜堰镇党委负责落实。各镇（区）可参照城区环境宣传方案进行布置。<br />
