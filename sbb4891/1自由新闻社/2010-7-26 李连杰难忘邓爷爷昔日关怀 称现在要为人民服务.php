　　“我从来都没讲过,在我最困难的时候,其实这些爷爷奶奶都在私底下关怀我很多。我从来也没敢拿他们当舆论炒作过。23年了,我没敢忘过每一句话。”在凤凰卫视前日刚刚播出的《鲁豫有约》中,李连杰首次披露了他刚成名时遭遇的困惑,曾向邓小平、杨尚昆、王震等国家领导人写信求助,并得到关怀和鼓励的往事。<br />
