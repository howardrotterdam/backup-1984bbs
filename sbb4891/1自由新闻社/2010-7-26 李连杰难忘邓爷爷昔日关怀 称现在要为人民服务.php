　　李连杰牢记邓小平教诲“长大为人民服务”<br />
<br />
　　少年成名,李连杰看见了比同龄人更大的世界,面对物质和金钱的冲击,李连杰心情异常苦闷。23年前,他斗胆给邓小平写了一封信:“我20岁的时候,我写信给邓小平爷爷,信的内容不便透露,写完之后我就去拍戏了。”李连杰没有想到,不出三个礼拜,有人到剧组通知他赶回北京去中南海见有关领导,“邓爷爷的批示有三句话:'想法很好、时机不成熟;好好学习;将来做个栋梁之材。’我没想到自己作为电影界的一个孩子,冲动地写了这么一封信,这么快就有了一个结果。”<br />
<br />
　　又过了近两个礼拜,有一个从中南海来的人转达了邓奶奶的祝福:“邓奶奶让我注意身体,长大为人民服务,可邓奶奶托他带给我的梨却忘记了。”李连杰还透露,后来自己还给时任国家主席杨尚昆和老一辈的革命家写过信,并且很快得到批示,“我现在做壹基金,就是要告诉几个老人家,我努力在为人民服务,为老百姓做多点事儿。”<br />
