													<h2>李一涉嫌强奸女大学生被重庆警方调查</h2>
						
						
																											<div id="postmessage_786744" class="t_msgfont">“神仙”李一涉嫌强奸被调查 履历神通统统造假<br />
[导读]重庆缙云山绍龙观住持李一，如今正被广泛质疑。他曾被宣传成养生专家、学问大师，号称有3万名弟子，现在却被曝光其履历和“神通”多有虚假，且被弟子举报强奸女大学生。<br />
<br />
神仙道长李一号称有3万信徒 曾涉三乱资金被查<br />
<img src="http://img1.gtimg.com/news/pics/hv1/201/233/592/38554416.jpeg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
李一不乐意与记者面谈，但愿意面对镜头身着道袍漫步竹林薄雾之中。 （叶雨/图）<br />
<br />
李一履历造假道籍仅4年 名人纷纷澄清与其关系<br />
<br />
重庆绍龙观住持李一的“神功”被指蒙人。<br />
<br />
重庆缙云山绍龙观住持李一，还是多年不执行法院赔付判决的被执行人，颇像是又一个在养生“造神”的高潮中被吹爆的气泡，更似又一张被掩盖了诸多真相的“画皮”。8月14日，李一的弟子到有关部门举报他涉嫌强奸等不法行为，15日，重庆警方介入调查。<br />
<br />
视频：李一道长“水下闭气”两小时视频曝光<br />
<br />
&gt;&gt;李一道长<br />
<br />
养生“大师”天价授课<br />
<br />
3岁就入道；在水下生存2小时22分，可以用脚后跟呼吸；能“辟谷”采天地灵气，停止胃酸分泌；可以用咒语和功力使人打开“中脉”，头顶骨自行裂开；有弟子3万，其中包括一批公众人物；主持着“养生圣地”，甚至当年张道陵、张三丰等名人也曾到缙云山养生……这些关于李一的宣传，无不勾勒出一位养生大师、当世高人的轮廓。<br />
<br />
想要得到他的指点，往往代价不菲。李一及其绍龙观开办了多个养生班，其中有一些平价班，如食宿费390元的“三日观”体验养生班；也有一些收费昂贵，如“5日班”每人学费3800元，“7日班”每人学费9000元。随着近年来“养生热”升温，在各类宣传推动下，上山找李一养生修行者纷至沓来，养生班屡屡爆满，甚至出现了“天价班”。<br />
<br />
2006年正式获得道籍<br />
<br />
李一究竟是何来历？记者从重庆市有关部门核实，李一原名李军，身份证号显示其出生于1969年9 月，原籍重庆巴南区，生于重庆沙坪坝区，初中文化，办过杂技团，经营过企业。1998年以来，李一先后率众重修了缙云山上的绍龙观和白云观，并成为其负责人，但正式获得道籍却是在2006年。<br />
<br />
当世高人身负“神通”<br />
<br />
除了一套和道学相关的养生理论外，李一及其弟子据称还身负“神通”。李一本人曾在电视上展示水下闭气生存2小时22分，其弟子还演示过嚼碎灯泡、钢针穿臂等“特异功能”。但出名后的李一除了为部分贵宾“通电体检”外，极少再做类似表演了，并声称“万般神通皆小技，唯有空空是大道”。<br />
<br />
一位曾接受李一“通电体检”的人士说，李一手持连接220伏电源的火线，让对方手持零线，再触碰对方手指，使其感到麻痹。他对于长年从事文字工作的这名人士的诊断是“要注意颈椎”。该人士回忆，李一的房间四壁挂着多张与各级领导干部的合影。但是，与其交谈时，一旦触及较深层次的学术问题，李一便会把话题避开。<br />
<br />
涉嫌强奸被警方调查<br />
<br />
8月14日，重庆缙云山绍龙观住持李一的皈依弟子常易(化名)到重庆市民宗委实名举报李一涉嫌强奸等不法行为。15日，重庆市北碚区警方正式介入调查举报中的“涉嫌强奸”。<br />
<br />
在常易给重庆市民宗委的举报材料中，有李一“在2004年或2005年左右涉嫌强奸西南师大的一位漂亮女大学生”的相关内容，常易还称绍龙观内有多人曾向其证明确有其事。<br />
<br />
8月15日上午，重庆市北碚区公安分局刑警队联系到常易，并将其带回北碚区公安分局协助调查。常易说，警方称民宗委将“涉嫌强奸”的举报转给了警方，警方已经正式介入调查。<br />
<br />
在长达一个半小时的笔录中，警方着重询问了有关李一“涉嫌强奸西南师大女大学生”的相关情况，常易称已将所知情况和当初向其证实此事的人名全部提供给了警方。<br />
<br />
询问常易后，警方称随后即到绍龙观调查，但未向记者提供进一步情况。<br />
<br />
&gt;&gt;李一弟子<br />
<br />
称神通乃意念作用<br />
<br />
记者日前来到绍龙观，发现身陷舆论漩涡的李一已谢绝访客。李一的弟子、缙云山道教协会副会长吴心的答复是，李一要“闭关”修炼，其间手机关机，不能会客，不能说话，“出关”时间不好说，几个月或更久都有可能。<br />
<br />
吴心说，李一表演的“水下闭气”是将密封容器放在水中，用“胎息”功夫在密封容器里闭气。之所以不能直接在水中闭气，理由是“胎息”时要专心，而直接在水里施功会受打扰。<br />
<br />
记者查阅李一当年表演“水下闭气”的录像资料发现，两名身穿公证员制服的人员仅表示他是“在密封容器中的时间共计为2小时22分”，而非水中闭气。<br />
<br />
“通电体检”是李一及其弟子们的又一项“神通”。吴心表示，它是操控220伏的电流，为体检者进行 “经络评估”，用任意一处插座都可进行，不需旁人暗中调节电量。“这是意念的作用。”<br />
<br />
&gt;&gt; 社会各界<br />
<br />
纷纷质疑“养生”泡沫<br />
<br />
许多被宣传为李一“弟子”的人士纷纷出来澄清。被传为“弟子”的凤凰卫视杨锦麟通过媒体声明： “既然我未曾皈依，何来是李一道长的弟子呢？”<br />
<br />
李一曾宣称，江西龙虎山的张道陵等历史名人都曾在缙云山养生，吴心也称典籍中有此记载，只是一时想不起是哪部典籍。对此江西龙虎山嗣汉天师府住持张金涛告诉记者，这种说法没有根据。他还表示，靠所谓的“小魔术”出名不是正道，最终会害了道教。<br />
<br />
操纵220 伏电是扯淡<br />
<br />
西南地区一位知名道教界人士说，李一所谓“3岁入道” 的说法很不客观，“通电体检”之类自己也不支持。<br />
<br />
“操纵220伏的电是‘鬼扯’！” 重庆第二十九中学物理教师钟崇华说，正常情况下人体安全电压约是36伏，这是基本物理常识。他说，以通电手法行骗并不稀奇，两人形成串联电路后，每人承受的电压降至110伏左右，就有可能不会当即致命；如果导线电阻很大，或再动些别的手脚，电流将更弱，就可能不会造成明显损伤。<br />
<br />
&gt;&gt;政法部门<br />
<br />
早年欠款仍未赔付<br />
<br />
重庆相关政法部门证实，李一早年经营企业时曾牵涉一笔大额欠款，被列为被执行人，但其多年来一直未执行法院的赔付判决。相关部门负责人称，李一在这笔欠款的赔付问题上钻了法律和政策的空子，导致判决难以执行。<br />
<br />
&gt;&gt;中医专家<br />
<br />
电击看病属“江湖神医”<br />
<br />
近年来，“排毒教父”林光常、“太医后人”刘弘章、“食疗专家”张悟本等“养生明星”先后倒下，显示出一方面社会健康需求增加，另一方面养生保健行业尚不尽规范，全社会的科学意识还有待增强。<br />
<br />
重庆市中医院内科专家郑劲说，市民养生不可盲目，应求助中医或西医营养学方面的正规机构和专家。目前的养生著作、讲座等大都系独立学者操办，多凭个人经验，不够系统，缺乏时间考验。至于“电击体检”，中医确有“从已病治未病”的理论，但所谓“电击就能预判今后五年疾病”则太玄了，已属“江湖神医”之流，个人不推荐。据新华社、《南方都市报》<br />
