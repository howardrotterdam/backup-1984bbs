<br />
　　这些变化总是让人想起赵本山的小品《卖拐》：范伟在赵本山的指导下大力踹地，终于感受到了自己腿部的酸麻。在科普学者方舟子看来这跟站桩是一个道理，“属于正常不过的生理反应”。<br />
<br />
　　“桩功，首先就是需要一动不动的，需要肌肉高度紧张，这种状态久了，产生颤抖也是一种自然生理反应。对于这个抖动反应能够治疗慢性气管炎、慢性肠胃炎、慢性肝炎、心脏病、癌症这些更是子虚乌有，完全是没有科学根据的。”方舟子告诉南方周末记者。<br />
<br />
　　8月1日，李一的弟子吴心道长，向记者演示了绍龙观的“秘传”通电疗法。她自己左手持声称是220V电源线的火线，让记者右手手持零线，然后用她的右手指逐一接触记者的左手手指，并“用内力控制”电流强度。<br />
<br />
　　“我检查的不是你身体现在的问题，而是5年内你身体哪些部位将会出现问题。”吴心道长说。<br />
<br />
　　在没有借助任何其他体检手段、仅通过电流点击记者手指之后，吴道长宣布，记者的心脏以及肺有问题。但具体是什么问题，吴道长语焉不详。<br />
<br />
　　8月2日，记者在重庆见到了此前曾跟随过李一一段时间的弟子。他告诉记者，“通电疗法”的秘密是，“插板有问题，有人在隔壁调控电量。所以吴心和李一，一直只在固定的那个位置，用固定的插板玩这个戏法。”<br />
<br />
　　次日，记者向常承提出，换一个地方重新做一次“通电体检”，常承拒绝了：“做一次通电体检要消费很多内力，吴心道长要休息一段时间才能恢复过来。”<br />
<br />
　　除了站桩、电检等独门秘法外，“早睡早起”、“生活规律”，“饮食克制”是养生班中主要的指导话题，对于这些有益无害的日常健康知识，班里的大部分修行者表示效果良好，对修行结果颇为满意。“清修效果感觉还可以呀。惟一的不太好是班主任好像年轻了一点。”自称学过营养学的、来自上海的于风带上高中的儿子一起参加了清修。<br />
<br />
　　在养生班里，总有几个特别积极的“仙友”，他们总是声情并茂地讲述自己曾经的病痛与皈依李一之后的神奇疗效。在记者的班里，一个自称来自天津的修炼者声称李一治好了折磨自己多年两次手术都无效的腰椎间盘突出。<br />
<br />
　　这样的养生修行所耗不菲：几乎可以形象地把绍龙观视作印钞机。“5日班”每班约30人，每人学费3800元；“7日班”每班约20人，每人学费9000元。还有面向富豪和达贵的“辟谷”、“闭关”服务，费用更达数万。尽管绍龙观的官方网站上声称一个月开班一次，但根据记者的实际观察，两种班都在像车轱辘连轴转一般开设。<br />
<br />
　　慕名而来满意而归的信徒们只是偶尔会谈到一些不能理解的地方。在北碚当地的一家酒店电梯里，两个身着练功服的北方中年男子认真地探讨：“李一师父说我们瘦一些好，但他比我们还胖啊。”<br />
<br />
　　两个人琢磨了半天，最后得出了一个结论：“师父的胖和我们的胖不一样。”<br />
<br />
　　从李军到“李一”<br />
<br />
　　他曾是杂技团长，找过野人，表演过特异功能，开过道医馆，还是诸多公司的股东和法定代表人。<br />
<br />
　　在李一的官网上关于他的描述是：原名李军，出生于1969年，道家世家，三岁入道。<br />
<br />
　　而记者调查得知，李一户口所在地为重庆市沙坪坝区石新路。其父为普通工人，其兄李世洪是重庆龙人按摩学校法定代表人。<br />
<br />
　　李一初为人知，是在1990年代初，彼时恰逢四川“巴蜀绝技大赛”，李一任团长的杂技团获得冠军，一时名声大噪。李一对杂技的喜爱保留至今，绍龙观拥有中国惟一的一个道教杂技团。<br />
<br />
　　除了杂技，李军似乎又作为“特异功能”人士行走过江湖。在上海电视台一档节目中，他端坐一个玻璃大缸里，在鱼儿环绕中水下闭气超过两小时，还请来了上海市公证处证明其功能的真实。<br />
<br />
　　利很快逐名而来。1993年，号称当时四川第二富豪（重庆尚未直辖）的重庆国光集团总裁刘宗朝，赞助李一“寻访南中国龙脉”的徒步行动。行经湖北神农架的时候，七十余家媒体跑来围观李一寻找“野人”。<br />
<br />
　　野人自然没有找到，但这是李一和企业家接触的第一次。刘宗朝据信是李一早期的重要资助者之一。知情人告诉记者，“寻访龙脉”之后，李一在重庆开了一家道医推拿馆，这是那段时期他的主业。据知情者回忆，道医馆就开在重庆市委大院旁边，这似乎佐证了知情者的说法：在与企业家搭上线之后，李一又积极向政治资源进行靠拢。<br />
<br />
　　1995年前后，李一开始了他人生的第一次转型。记者从重庆市相关部门获取的资料显示，在1998年，李一已经是润达扶贫有限公司、国立股份有限公司、龙人集团、无量水厂等企业的重要股东或法定代表人。<br />
<br />
　　1998年，在商场面临困境的李一，开始涉足宗教。29岁的他盯上了缙云半山的绍隆寺。他开始寻找资金，改建绍龙观。在后来的自述中，李一对这一阶段自己的身份描述是“道士”。但记者调查得知，李一成为真正的道士，是在他于缙云山大兴土木修建道观8年之后。<br />
<br />
　　依据中国道教协会1998年8月24日第六届代表会议修改通过的《关于道教散居正一派道士管理暂行办法》和随后颁布实施的《道教教职人员认定办法》，成为道士，必须拥有中国道教协会统一印制的《道教教职人员证书》。<br />
<br />
　　而要获得《道教教职人员证书》，必须具备的一条是：“有传统的道教师承法派。”在绍龙观的祖师堂，悬挂着放大的一幅彩色照片。照片上记录的是，李一皈依上海城隍庙陈莲笙道长门下获得教籍的一刻。<br />
<br />
　　陈莲笙道长已于两年前离世，所以无法获悉4年前李一皈依时的具体过程。但8月3日，记者从上海城隍庙管理处确认了2006年李一在上海获得《道教教职人员证书》的事实。<br />
<br />
　　吊诡的是，依据《道教教职人员认定办法》，教职人员应该是向“所在地道教协会提出”认定许可。即，李一应该是向重庆市道教协会提出申请。但他却到千里之外的上海完成了这一程序。<br />
<br />
　　“时任重庆市道教协会会长的周至清道长，非常反感李一借道教敛财，所以一直拒绝给他道籍。”上述知情者告诉记者。只是，周道长已于2008年去世，这些旧事，已无法核实。<br />
<br />
　　但毫无疑问，从获奖杂技团长到企业家，再到宗教界人士，李一的一次次华丽转身中，这一次，毫无疑问是最成功的。1998年，当时还叫李军的商人，带着一队农民工来到缙云山上破落的绍隆寺，把这佛寺改成了道观，起名绍龙观。历史上缙云山实为佛教名山，绍隆寺建于明代，民国期间曾为收养抗战孤儿的北碚慈幼院。2005年，李军对山顶的白云寺也如法炮制，起名白云观。在宣传材料中，李一却阐述了缙云山与道教的渊源，根据他的说法，张天师与张三丰都曾在缙云山修道。在修筑了两所道观，获得了教籍，并把缙云山打造成为道教圣地的商人李军，自此变成了今日的道长李一。<br />
<br />
　　种种神迹与盛会随即纷至沓来。坊间传言，在绍龙观修建之初，“柱子下就挖出了蛇和乌龟”，在道教中这被看做是罕见的吉兆；道观门口长时间摆着一台电视，反复播放李一在各路电视节目中接受访谈的画面；2005年2月，他甚至广发“武林帖”，在白云观召开武林大会，尽管据当时参加了武林大会的记者回忆“打狗棒、降龙十八掌全出来了，有点像闹剧”，但绍龙观还是宣称获得了“齐名”少林寺的名声：“天下功夫出少林，天下绝技在缙云”。<br />
<br />
　　“修仙”之路<br />
<br />
　　“龟息闭气，双手导电，嚼碎灯泡，钢针穿臂”，李一对此的解释基础既有道家经典，也有分子医学，还涉及量子物理。<br />
<br />
　　李一成为道士后的四年，毫无疑问是他人生最灿烂的四年。这几乎是一个奇迹，其盛名恰可用一句道家经典形容：一生二，二生三，三生万物。<br />
<br />
　　最初的信徒据说来源于一批名人。2008年，樊馨蔓的好友马云，包括王菲李亚鹏开始抵达绍龙观修行。樊馨蔓甚至撰写了一本名为《世上有没有神仙》的书来介绍李一和他的养生方法。<br />
<br />
　　在宣传材料中，马云、杨锦麟、王菲、李亚鹏都成了李一的弟子。在缙云山进山公路上，绍龙观一度拉起横幅彰显王菲李亚鹏夫妇对绍龙观的信任，当地人回忆，绍龙观的道士们一度神秘而又得意地对信徒们宣称，王李夫妇正是在山上清修后才怀上了孩子。<br />
<br />
　　2010年6月，才当了不到4年道士的李一当选中国道教协会副会长。<br />
<br />
　　在绍龙观的官方网站上，李一所获得社会荣誉以及学界认可包括：受聘于华中师范大学历史文化学院兼职教授、马来西亚吉隆坡中医学院教授，在复旦大学管理学院、北京大学哲学系、浙江大学、剑桥大学、牛津大学、雷丁大学、伦敦大学、诺丁汉大学等国内外知名院校讲授国学和养生文化。<br />
<br />
　　但记者随即调查发现，以上信息，多为虚假。“北京大学哲学系并没有一个叫李一的兼职教授或者讲座教授。”北京大学哲学系的谢姓老师告诉南方周末记者。她是2001年入职北京大学哲学系的工作人员，她表示自己入职以来，从未有过名为李一的道长来哲学系交流或者讲学。“李一只是国学班在一次论坛中请来的嘉宾，并没有给国学班上过课，国学班只是同北大有合作关系，并不隶属。”北京大学国学班的工作人谷先生告诉记者。国学班一般给企业领袖和成功人士提供培训。<br />
<br />
　　记者从复旦大学了解到的情况和北大类似。复旦大学管理学院师资办公室的黄小姐是管理学院2006年入职的工作人员，她在自己的工作生活中从来没有听说过李一这样一个人，学院的资料库中也没有任何关于李一的信息，“一般来说学院邀请的讲学嘉宾是会有登记的。”<br />
<br />
　　而剑桥大学相关学院则一口否认他们有李一这样一个客座教授。<br />
<br />
　　惟一确定的是2006年，李一到华中师范大学的一行。“2006年左右，李一到华师来过一次。当时就是跟学院领导见面、吃饭这些事情，大家也算是熟人了。”华中师范大学历史文化学院道家道教研究中心熊铁基主任说，“但中心和学院也并没有任何书面的聘书给李一，或者任何聘任李一为兼职教授的书面材料。而且自从那一次见面之后，李一就再也没有来过华师，也没有给学生讲过课。”<br />
<br />
　　熊铁基回忆，当时李一给学院的领导和教授们展示了他的“绝技”，比如以手接220伏电“运功”治疗术。但是自己对这个东西“不太相信”。<br />
<br />
　　王玉德是李一来华中师范大学时的历史文化学院院长，“因为学院有不少是做道教研究的，当时李一来到学院做了一个报告，讲的是道德经的标点问题，个人认为他还是有学识的。”王玉德强调，学校是绝对没有聘任李一的，更没有聘书。但王玉德不能确定是否有下面的系和研究机构聘任李一，“对于李一和华师的关系，还要看到资料最后确认。”<br />
<br />
　　不只是提供纠结的、真伪难辨的头衔给他的潜在信徒，李一还通过对以前一些表演的模糊叙述，给信徒以他“道行高深”的印象。<br />
<br />
　　李一津津乐道的是他的一项吉尼斯世界纪录：1997年1月19日，在上海电视台《天下第一》栏目中使用道家胎息法，创造水下闭气生存2小时22分钟的吉尼斯世界纪录。<br />
<br />
　　记者调查得知，所谓的“水下闭气生存”，其实是李一呆在一个充满空气的玻璃箱中，然后放进另外一个更大的玻璃缸中，在玻璃缸中注水，水淹没密闭的玻璃箱，再在水中放上金鱼，造成水下闭气的假象。<br />
<br />
　　李一的一个弟子向记者承认了上述表演方法，但他辩解说：当年师父只是说自己在玻璃箱子里闭气，水中闭气是外间的以讹传讹。<br />
<br />
　　对于普通信众，李一则常命几个大弟子演示“神迹”，在《世上有没有神仙》一书中就曾记录，一位女弟子当众嚼碎电灯泡吞下，还能钢针穿透手臂不留伤痕。而李一对此的解释则玄之又玄，其复杂理论中既有道家经典，也有分子医学，还涉及量子物理。<br />
<br />
　　起到巨大广告效应的名人们现在似乎不肯为“师父”的神奇背书了。凤凰卫视主持人杨锦麟承认去缙云山休息过几天，但对于“弟子”的称号很是不满：“如果我去咖啡厅喝茶，刚好里面贩毒，那我就是毒贩吗？”<br />
<br />
　　马云是被朋友樊馨蔓“强力劝说”上山“闭关”的。尽管他曾把李一定位为“难得的青年道家研究者”，但现在对于养生治病等种种技巧他“一概不感冒”，他觉得仅仅关注这些表象就是“走歪了”。<br />
<br />
　　凤凰卫视执行台长刘春则直接否认了“弟子”的名号，“慧根有限，不敢高攀。”他解释说。<br />
<br />
　　但直到现在，名人的招牌以及各种“神迹”的吸引力，仍然是全国的信徒们远来缙云山的原因。如今，李一门下“弟子”号称三万，上山修炼者已是不计其数。<br />
<br />
　　“李一是我这一生中遇到过的最能让我感动的人之一。”7月底，从海南来绍龙观参加养生班的梁青山说，“广东万形县有一位上师在山里修行三年，特别有德行，使山放红光。可以时空预制。”在他看来，李一和广东这位上师一样伟大。<br />
<br />
　　梁青山最初是通过网络认知李一的。“樊馨蔓持续在博客中记录与李一道长的仙缘，我越看越感动，所以就来了。我们祖国文化让我们人人都有信仰，让我们人人都自主。”<br />
<br />
　　“被执行人”李一<br />
<br />
　　绍龙观的修建资金中，有260万来源于一笔已经拖了7年的赔付判决。<br />
<br />
　　在李一和绍龙观的诸多盛名当中，最不为人所知的应该是在重庆司法机关中的“名声”。<br />
<br />
　　“如果不是因为绍龙观是宗教活动场所，我们早就把它查封拍卖了。”当年曾调查过李一涉及“三乱资金”一案的民警告诉记者。<br />
<br />
　　在重庆当地司法机关看来，绍龙观的名气，不在于他的养生法，也不在他齐名少林的绝技，而在于他给司法界出了个难题：当宗教活动场所成为被执行人该怎么办？<br />
<br />
　　2005年，大渡口区经侦支队接到了大渡口区人民法院转来的一个“执行难”的案子：注册在辖区内的润达扶贫有限责任公司拒绝执行一笔已经拖了7年的赔付判决。<br />
<br />
　　经侦队调查发现，润达扶贫公司的实际控制者，早已变更为李一。经过一年的侦查后，经侦部门发现，“执行难”的原因在于，自1998年起，润达扶贫有限公司累计1000万元，被李一以各种方式挪作他用。<br />
<br />
　　其中最大的一笔260万，便是用来修建绍龙观。<br />
<br />
　　“但是我们发现，与这1000万元有关的公司，都已经是注销或者破产，就只剩下绍龙观”，“可以说李一是一个资金运作高手”，一个当年参加调查此案的民警说。<br />
