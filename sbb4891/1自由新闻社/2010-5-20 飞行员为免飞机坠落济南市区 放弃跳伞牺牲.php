													<h2>飞行员为免飞机坠落济南市区推迟跳伞牺牲</h2>
						
						
																											<div id="postmessage_626929" class="t_msgfont"><img src="http://img1.gtimg.com/news/pics/hv1/122/106/527/34295327.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
5月6日晚，济南空军某师一架飞机在连续起飞过程中发动机突然停车，有坠落在济南市区的危险。为避免造成重大事故，两名飞行员尽力改变飞行轨迹，其中飞行员冯思广因错过最佳跳伞时机牺牲。<br />
五载峥嵘岁月为国奉献，二十八年风华正茂为民献身。连日来，泉城济南处处传颂着济空飞行员冯思广舍身捍卫人民群众生命和财产安全的英雄事迹：5月6日晚，冯思广和中队长张德山驾机在连续起飞过程中发动机骤然停车，为避免飞机坠落在济南人口稠密地区，冯思广把个人生死置之度外，和张德山一起果断改变飞行轨迹，自己却因错过跳伞最佳时机英勇牺牲。“他用28岁的宝贵生命，书写了一曲忠诚于党、热爱人民的时代颂歌！”冯思广所在济空航空兵某师师长詹仁明介绍，5月6日，该师在济南组织跨昼夜飞行。20时51分，飞行员冯思广和张德山驾机起飞。21时30分，第二次着陆连续起飞高度约50米，发动机声音骤然减小，推力迅速下降，后舱飞行员张德山急促向地面报告“我停车了”，飞行指挥员、师参谋长沈树范迅即果断发出指令：跳伞！跳伞！<br />
<br />
千钧一发之际，冯思广和张德山十分清楚：飞机前下方不远处，就是一大片居民区和夜市。如果不改变飞行轨迹，后果不堪设想！<br />
<br />
生死关头，冯思广和张德山只有一个念头：最大限度地保障人民群众的生命和财产安全。他们没有立即弃机跳伞，不约而同地前推驾驶杆，将飞机由仰角12.3度迅速推至俯角9.8度，看到飞行轨迹已经避开居民区，才实施了跳伞。飞参记录显示，飞行员跳伞前，驾驶杆前推44毫米，由此改变了飞行轨迹。<br />
<br />
按照飞机坐椅弹射程序，后舱先于前舱弹射，间隔为1.1秒。就在这1.1秒之间，先行跳出的后舱飞行员张德山跳伞成功。而前舱飞行员跳伞时飞机高度仅有32米，且带有16度俯角，低于弹射安全包线高度，弹射后降落伞未张开即坠地，前舱飞行员冯思广壮烈牺牲。<br />
<br />
机组一次生死抉择的“推杆”，改变了飞机的飞行轨迹，改变了一名年轻飞行员的生命航向，保护了人民群众的生命和财产安全。据调查，飞机坠落地点与居民区直线距离为230米，附近有村庄、宾馆、工厂、市场、物流站，常住人口约4000人。如果不是两名飞行员临危不惧的正确处置，将会给省城带来不可估量的重大损失。“在生死关头最大限度地保护国家和人民群众生命财产安全，是人民空军每一名飞行员浸透到骨子里的信念！”济空航空兵某师政治部主任姜录臣介绍，他们以空军部队近年来涌现出的“蓝天英雄”梁万俊、李中华、李剑英、李峰和孟凡升等先进事迹为生动教材，不断深化飞行人员的当代革命军人核心价值观教育，使飞行员在处置重大空中特情时真正做到上不愧天、下不愧地、内不愧心，视人民群众利益高于一切、重于一切。 5月7日被批准为革命烈士的冯思广，1982年1月出生于山东省聊城市茌平县肖家庄乡冯营村，2001年9月考入山东理工大学生物工程专业，2005年6月招飞入伍，2007年6月毕业于空军第三飞行学院，2009年荣立三等功，是一名爱家乡、爱亲人、爱战友、爱人民、爱社会的新一代优秀飞行员。<br />
<br />
冯思广和空军某场站医师田文君已于2009年8月领取了结婚证，几次计划举行婚礼，都因冯思广加紧进行改装训练而未能如愿。在5月10日上午的追悼会上，田文君以她和冯思广的名义，分别给济空司令员刘忠兴中将敬了两个军礼，颤抖的右手久久没有放下。田文君说，“请司令员见证，今天就是我和思广的婚礼……这辈子，嫁给这样的飞行员不后悔……我一定替思广为父母尽孝，让他在天堂无牵挂……”<br />
<br />
中央军委领导获悉后深情称赞：“虽然冯思广同志牺牲了，但那种视死如归、英勇无畏、对人民大忠大爱的精神永远值得我们学习！”冯思广烈士的英雄事迹在空军部队引起深刻反响，成为广大官兵深化当代革命军人核心价值观教育的生动教材。空军飞行员们表示，要像冯思广那样热爱祖国、热爱人民，为构建和谐社会倾心竭力。(来源：济南日报 通讯员 申进科 陈卫华 崔桂顺 记者 孙勇)</div>

							
							
							
							
