													<h2>国务院正式批复北京市部分行政区划调整</h2>
						
						
																											<div id="postmessage_715835" class="t_msgfont">2010年07月01日 16:58:31&nbsp;&nbsp;来源： 新华网<br />
<br />
新华网北京7月1日电（记者孙晓胜）近日，国务院正式批复了北京市政府关于调整首都功能核心区行政区划的请示，同意撤销北京市东城区、崇文区，设立新的北京市东城区，以原东城区、崇文区的行政区域为东城区的行政区域；撤销北京市西城区、宣武区，设立新的北京市西城区，以原西城区、宣武区的行政区域为西城区的行政区域。<br />
<br />
批复要求北京市要尽快明确新设区政府驻地位置，并按程序报批。行政区划调整涉及的各类机构要按照“精简、统一、效能”的原则设置，涉及的行政区域界线要按规定及时勘定，所需人员编制和经费由北京市自行解决。要严格执行中央关于厉行节约的规定和国家土地管理法规政策，加大区域资源整合力度，优化总体布局，促进区域经济社会协调健康发展。<br />
<br />
根据北京市区县功能定位，首都功能核心区包括原东城、西城、崇文、宣武四个中心城区。合并后新设立的东城区，辖区范围为现东城区和崇文区辖区范围，面积41.84平方公里，常住人口86.5万人。合并后新设立的西城区，辖区范围为现西城区和宣武区辖区范围，面积50.70平方公里，常住人口124.6万人。<br />
<br />
据介绍，北京市此次调整行政区划，一是有利于贯彻落实科学发展观，推进区域均衡发展。核心区四个区面积共92.54平方公里。由于行政区划划分过细，四个区的发展受到空间因素的制约，特别是崇文区和宣武区发展受空间因素制约更为明显，经济社会发展水平滞后于核心区北部。通过核心区行政区划的合并调整，有利于对现有的空间资源进行有效整合，推进核心区南北均衡发展。二是有利于提高核心区的承载能力和服务水平。通过核心区区划的合并调整，可以大幅度提高合并后的两个区的综合实力，进一步拓展发展空间，有利于提高核心区的承载能力和服务水平，也有利于解决四个区结合部管理薄弱的状况。三是有利于加强历史文化名城的整体保护。根据《北京城市总体规划（2004年-2020年）》和专项规划，核心区与旧城保护区基本重合。旧城保护任务由四个区承担，不利于统一建设、管理和整体保护。区划调整后可以集中力量加快老城区改造，加大历史文化名城保护力度。四是有利于降低行政成本，提高行政效率。行政区划调整后，核心区减少两个行政区，有利于精简机构，合理配置地区经济、社会资源，提高行政效能，降低行政成本，提升核心区规划建设和整体管理水平，增强“四个服务”的能力，以更高的标准推动“人文北京、科技北京、绿色北京”建设，把首都功能核心区打造成为建设中国特色世界城市的示范区。</div>

							
							
							
							
