																											<div id="postmessage_311002" class="t_msgfont">零八宪章签署人再次集体荣获国际民主人权奖<br />
<br />
发表日期 02/11/2009 更新日期 02/11/2009&nbsp;&nbsp;11:59 TU<br />
<br />
设在美国旧金山的中国民主教育基金会“中国杰出民主人士奖”定于11月14日在美国旧金山举行颁奖仪式，今年的这一奖项授予《零八宪章》所有签署人。这是《零八宪章》自签署不到一年的时间里，签署人第二次集体荣获国际社会颁发的民主人权奖项。<br />
2008年12月，303人首批签署了由中国独立作家刘晓波起草的《零八宪章》，很快就有第二批、第三批签署人加入行列，从中国知名人士到普通百姓，至少有上万人以自己的签名表达对《零八宪章》的赞同与公开支持。<br />
<br />
中国民主教育基金会会长周峰锁10月29日说，《零八宪章》发表后受到中共当局的全面镇压，刘晓波遭逮捕拘押，被当局传讯的签名人有上千人，“但迄今为止，我们没有看到任何一个签名人声明退出《零八宪章》，不管政府以失去自由威胁或者以暴力威胁，没有一个人放弃《零八宪章》”。<br />
<br />
有评论认为，由于据理抗争，所以迫使当局淡化处理《零八宪章》，以免扩大其在中国民众中的影响力。从刘晓波的律师莫少平律师事务所传出来的消息说，官方没有追究刘晓波《零八宪章》的事，而是说他以前发表的一些文章有煽动颠覆的内容，这说明官方在刻意地回避《零八宪章》，把《零八宪章》淡化处理。”《零八宪章》签署人之一、深圳作家赵达功说，中国当局现在采取鸵鸟政策，不准任何人提《零八宪章》，也不说明《零八宪章》到底有什么问题。他说，中国社会矛盾的日益激化就会使更多的人勇敢地站出来，此次获奖让所有《零八宪章》签署人感到自豪，尽管当局以各种手段企图让签署人感到恐惧。<br />
<br />
赵达功希望中国当局注意到国际社会对《零八宪章》的关注，对《零八宪章》签署者的关心。他向美国总统奥巴马发出呼吁，在奥巴马11月份访华期间应当促成《零八宪章》起草人刘晓波获释。<br />
<br />
2009 年3 月，捷克People in Need将2008年的Homo Homini人权奖发给《零八宪章》起草人刘晓波和全体《零八宪章》签署人。《零八宪章》签署人莫少平、徐友渔和崔卫平女士前往捷克，代表仍然被中国当局秘密关押的刘晓波、代表其他签署人，从捷克前总统哈维尔手中接过了这项发给为人权事业做出杰出贡献的人士的人权奖。<br />
<br />
哈维尔是著名的《七七宪章》起草人。他在颁奖仪式上代表大多数《七七宪章》签署人表示，对七七宪章能够激励中国的《零八宪章》感到高兴与荣幸。<br />
