													<h2>１１８．著名的“ＢＢＣ六四广播讲话”</h2>
						
						
																											<div id="postmessage_339308" class="t_msgfont">　　１９８９年６月４日清晨５时，杨宪益的家门被一个失魂落魄的青年人撞开，正是这个人第一个向杨宪益报告了他亲眼目睹的惨剧。此人名叫杨立行，是个新西兰华侨。他的父亲杨ＸＸ老先生是个有名的爱国华侨，全国政协委员，为了支持祖国建设，他把两个儿子(杨立行是老大)都派回国内，分配到外文局工作。读者当还记得，正是这个杨立行在文革初期曾当众揭发过杨宪益的“反对毛主席”的“现行反革命言论”，使杨宪益的批斗会骤然升级。文革后期，杨立行曾回新西兰数年，中国的改革开放使杨立行再度萌发回国效力的念头，于是，已入了新西兰籍的杨立行再一次来到中国。这一次，他作为“外国专家”在中国社会科学院工作。文革使杨立行大彻大悟，他非常后悔对杨宪益做过的事情，曾多次当面痛哭着向杨宪益道歉。杨宪益从来就没有记恨过他，他们重又成了好朋友，并时有往来。６月４日清晨，杨立行的模样使杨宪益吃了一惊，他在《自传》中写道：<br />
<br />
 <br />
<br />
&nbsp; &nbsp; 他的神色狂乱而悲伤，面色苍白，衣服、头发散乱，极度的悲哀和恐怖使他的精神已近乎歇斯底里，说话断断续续、语无伦次。我给他倒了一杯咖啡使他镇静下来。这个年轻人文化大革命以后重新回到中国，以为一切都朝着好的方向发展。那天早上，他在愤怒之中不停地自言自语说“政府怎么可能？！他们怎么会……屠杀自己的人民？！”似乎仍不相信己发生的事情，他告诉我，那天晚上他进城去看广场的学生，还没到天安门广场，屠杀就开始了，他看见有人中弹，倒在马路边；他还亲眼看见一个女学生被近距离的子弹轰穿了眼睛。……（杨宪益:《白虎星照命》，２８５页，雷音译）<br />
<br />
 <br />
<br />
&nbsp; &nbsp; 政府的公告说北京在６月３日发生了“反革命暴乱”，所以解放军开枪是为了“平息暴乱”，“恢复首都秩序”。对于这一谎言，杨宪益在《自传》中是这样批驳的：<br />
<br />
 <br />
<br />
&nbsp; &nbsp; ６月３日晚上和６月４日凌晨的“天安门屠杀”发生以后，政府为了掩盖他们的骇人听闻的罪行，声称整个北京都发生了暴乱，“暴徒殴打解放军战士，放火烧毁公共汽车和电车，引起巨大破坏。因此，军队不得不开进城里恢复秩序。而实际上，直到６月３日北京仍然处于平静、甚至于节日般的气氛之中。尽管有谣传说镇压快要临近了，但是北京市民们做梦也想不到他们自己的政府竟然会如此对付他们！大约在半夜，当我们听到通往市中心的主要街道上传来隐隐约约的枪声时，还有人以为是中国人在喜庆时刻燃放的炮仗声。然而我们终于得知――他们竟然真的开枪杀死了试图阻止军队前进的市民和学生！那一夜，北京没有一个人能够睡一个踏实觉。黎明时分，枪声渐渐平息，仅有零星枪响从市区各个方向传来。接着，各种各样的人都来报告：许多人在长安街上被枪杀了；士兵们在大街上巡逻，并随意向胆敢冒险上街的人们开枪射击。有的士兵甚至向居民家的窗户开枪，因为窗户里有人向外张望。一些无辜的人――包括儿童――就这么在自己家里被杀害了。（杨宪益：《白虎星照命》，２８４页，雷音译）<br />
<br />
 <br />
<br />
　　杨立行的控诉和许多见证人的叙述给杨宪益极大的刺激。和杨立行一样，正因为对改革开放以后的中国共产党抱有深深的信任和希望，杨宪益才会对中共犯下“杀戮手无寸铁的人民”的罪行感到加倍的愤恨。在杨宪益的一生中，很少出现情感无法控制的时刻，可是，６月４日上午，当听完杨立行的叙述后，杨宪益的心胸溢满了无法排解的悲愤和狂怒。正在这时，远隔千里的伦敦打来了长途电话，对方是英国ＢＢＣ广播公司的记者，问杨宪益对“六四事件”的看法，于是，杨宪益对着话筒倾诉了他的满腔义愤。后来，ＢＢＣ反复播放６月４日对杨宪益的采访录音，使这篇广播讲话流传很广，成为著名的来自中国民间的“抗议檄文”。杨宪益的ＢＢＣ广播讲话不长，总共只有两分钟时间，原文（大意）如下：<br />
<br />
 <br />
<br />
　　这是中国现代史上最可耻的事！这样的血腥屠杀过去的任何反动政府都没有干过，北洋军阀、国民党政府都没有杀过那么多的手无寸铁的无辜百姓。甚至连日本侵略者在占领北京的时候也没有干过这种事！很多人都亲眼目睹了这场杀戮。见证人看到一个７岁的小女孩在人民大会堂外被枪杀；还看见女学生的眼睛被子弹打穿；有的人被打掉一半脸；学生并非在抵抗，他们只是手挽手地企图挡住军队的推进，但是士兵就用机枪胡乱扫射，学生走开的时候，军队还从后面追着向他们开枪。<br />
<br />
 <br />
<br />
　　这些解放军是邓小平和杨尚昆的个人军队，这些人和人民解放军没有什么关系，这些军人也是一些无辜的人。责任应由为首的邓小平、杨尚昆和李鹏来担负。他们犯下的罪行，将作为中国历史上最恶劣的犯罪行为，永载中国的史册。<br />
<br />
 <br />
<br />
　　（记者：你显然是在批评政府，这样公开的批评政府对你个人是否有危险？）<br />
<br />
 <br />
<br />
&nbsp; &nbsp; 杨回答：我所说的并非批评，而是对罪犯的一种谴责。法西斯分子杀人不眨眼，屠杀了许多人，他们可以将我也加在杀害的名单之上，但是他们不能够杀光我们所有的人。<br />
<br />
 <br />
<br />
&nbsp; &nbsp; 我不知道事态将会怎样发展，但是，他们不可能摧毁整个国家。<br />
<br />
 <br />
<br />
&nbsp; &nbsp; （根据录音和１９８９年６月５日香港《大公报》的报道）<br />
<br />
 <br />
<br />
&nbsp; &nbsp; 录音播放以后，许多人通过收音机听到了杨宪益的声音。那时，正是政府大肆“秋后算帐”的时侯，整个社会处于一片万马齐喑之中，杨宪益的讲话就像黑暗中的一盏灯，照亮了许多人的心。也正是在这个时刻，“杨宪益”这三个字，被印在了许多中国人的心中。<br />
<br />
 <br />
<br />
&nbsp; &nbsp; 对于讲话播出后的反应，杨宪益在《自传》中写道：<br />
<br />
 <br />
<br />
&nbsp; &nbsp; 几天后，我从朋友们那里得知，他们通过ＢＢＣ的广播听到了我的讲话，声音响亮而清晰。许多人甚至把讲话录音复制了下来。我的广播讲话流传很广，反响强烈。我感到很高兴。（杨宪益：《白虎星照命》，２８６――２８７页，雷音译）<br />
<br />
 <br />
<br />
&nbsp; &nbsp; 上述自白又一次证明，杨宪益的为人准则是听凭“天性”而不是听凭“利害关系”。他尽管并不想充当英雄，但由于行事方式是“恃欲轻言”（这是他对自己的评价）而不是“瞻前顾后”，所以，他往往在无意之中做出一般人做不出的举动来。</div>

							
							
							
							
