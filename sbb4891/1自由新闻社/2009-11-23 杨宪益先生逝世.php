<br />
&nbsp; &nbsp; 11月23日，著名翻译家、外国文学研究专家、文化史学者和诗人杨宪益先生今日逝世，享年九十五岁。<br />
<br />
&nbsp; &nbsp; 11月23日下午4点，北京资深媒体人萧三郎在其新浪微博发布消息，“杨宪益去世”。《中国图书商报・阅读周刊》记者，网站执行主编江筱湖同时也确认了这条消息。江筱湖说，“杨宪益老人，今晨去世。默哀。”<br />
<br />
&nbsp; &nbsp; 杨宪益(1915-2009)著名翻译家、外国文学研究专家、文化史学者和诗人。祖籍安徽盱眙(今属江苏省淮安市)鲍集镇梁集村，生于天津。据自传体《漏船载酒忆当年》，他出生于戊寅年冬月二十七(即公历1915年1月10日，阴历1914年11月27日)。1934年在天津英国教会学校新学书院毕业后到英国牛津大学莫顿学院研究古希腊罗马文学、中古法国文学及英国文学。抗日战争时与吕叔湘、向达等友人在伦敦华侨中作救亡工作，出版中文报纸。1940年回国任重庆大学副教授。1941至1942年任贵州贵阳师范学院英语系主任，1942年至1943年任成都光华大学英文教授，1943年后在重庆北碚及南京任编译馆编纂，1947年把在重庆北碚时所写的文史考证文章编辑成一个集子，由卢前(冀野)起名为《零墨新笺》，是为“新中华丛书”的一种。后来在解放期间又写了一些，于1949年解放后把后写的这部分编成一集，自费印了100册，名为《零墨续笺》。在1983年6月三联出版社将《零墨新笺》和《零墨续笺》合起来出了一本书，名为《译余偶拾》印刷9500册。1953年调任北京外文出版社翻译专家，曾与夫人戴乃迭(英籍中国文化学者)合作翻译中国古典小说《魏晋南北朝小说选》、《唐代传奇选》、《宋明平话小说选》、《聊斋选》、全本《儒林外史》、全本《红楼梦》等，均先后由北京外文出版社出版。六十年代初，杨宪益和戴乃迭夫妇开始翻译《红楼梦》，其间曾一度中断，最后于1974年完成，于1978～1980年由外文出版社分三卷出版，译文书名为“A Dream of Red Mansions”。上述各种译本在国外皆获得好评，并有广泛影响。1993年在香港大学获得名誉博士学位。<br />
<br />
&nbsp; &nbsp; 写有英文自传体《白虎星照命》，意大利文译本书名为《从富家少爷到党员同志》，中文译名《漏船载酒忆当年》，于2001年4月由北京十月文艺出版社作为“百年人生丛书”出版，薛鸿时译，印刷10000册。</div>

							
							
							
							
