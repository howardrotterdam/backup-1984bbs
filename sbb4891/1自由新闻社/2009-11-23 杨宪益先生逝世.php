下面是一篇书评<br />
<br />
枫晴：读《杨宪益传》<br />
　　――情深更莫醒 醒莫更深情<br />
　　<br />
　　<br />
　　这又是一本“不如烟”的传记。从杨老手里接过这本书的时候，我并没有意识到书是这么沉这么重的。<br />
　　<br />
　　本文要写的并非杨老正式出版的二十多万字的英文自传&lt;&lt;白虎星照命&gt;&gt;，而是化名“雷音”的作者在历时七年的资料收集采访后，“因种种原因无法公开出版”而“自费印刷少量以表达对杨老敬意”的&lt;&lt;杨宪益传&gt;&gt;。没有等到雷音说的“但愿此书有朝一日能公开面世”的那一天，大陆的地下渠道书商已经迫不及待地把书畅销开了。<br />
　　<br />
　　<br />
　　哀莫大于心不死<br />
　　<br />
　　提起”杨宪益”这个名字，人们多只会想到他是当代中国著名的翻译家、《红楼梦》的译者，而雷音在&lt;&lt;杨宪益传&gt;&gt;的第一段则呈现了杨老更应该为世人所知的一面：<br />
　　<br />
　　“19，89年，6月，4日，驻北京的戒/严/部/队/向/手/无/寸/铁的民众开/枪以后，外电广播了一个中国知识分子的谈话录音：‘我谴责戒/严/部队屠/杀/北京市民的罪行！中国人民是杀不绝也吓不倒的！他们可以将我也加在杀害的名单之上，但是他们不能够杀光我们所有的人！他们不可能摧毁整个的国家！’……人们永远记住了他的名字 -- 杨宪益。” （第3页）<br />
　　<br />
　　历尽沧桑仍然没有倒下的杨宪益通过BBC电台道出了他这几句掷地有声的抗议后，他的妻子却彻底地倒下了 C 六/四/后“整天担惊受怕度日如年”地担心着杨宪益安危的她在大病一场后失去了记忆，丧失读写能力，再也没有恢复过来，直到离开人世（第395页）。这位妻子，并非软弱之辈，而是牛津大学首位取得中国文学荣誉学位的毕业生、半个多世纪前不顾母亲极力反对与未婚夫杨宪益踏上了战火纷飞的中国土地、在“刨祖坟、查三代、断绝一切海外关系”的年代依然没有离开夫君的英国女子戴乃迭(原名Gladys Margaret Tayler, 戴乃迭是中文名字) 。这位与中国普通老百姓共同经历了抗战内战的艰难日子、饱受过无休止的阶级斗争和“非我族类”的折磨、在“文革”被脱光衣服搜身然后被判单独囚禁四年之久、几十年来与杨宪益联手翻译了一千多万字作品而成为“译界泰斗”的坚强女子，到了1989年，似乎已经无法再一次承受作为妻子为丈夫的安危担忧的恐惧，连带着她作为母亲对因“文革”种种迫害而自杀身亡的爱子的无限的歉意，重重地倒下了。<br />
　　<br />
　　&lt;&lt;杨宪益传&gt;&gt;的扉页有两张对比鲜明的照片：一张是戴乃迭和杨宪益牛津时代在英国湖区的合影，照片中的戴杨都穿着高领毛衣外披有着浓浓欧洲风格的长风衣，戴乃迭漂亮高挑气质高雅，这对热恋中的情侣含羞浅笑，我在隔了大半个世纪后依然可以感受照片里洋溢着的浪漫深情；另一张是戴杨两人在历尽磨难后的合影，两位老人白发苍苍，虽然面露笑容，看了还是让人心寒心酸，单单岁月，是不会留下如此痕迹的。这样强烈的对比，我在读老威的《底层访谈录》有过：《底层》里有一张胡风和夫人梅志在三十年代的照片，两人写在脸上的理想和希望深深打动了我；翻过书的另一页，是胡风梅志在六十年代的一张表情麻木的合照，要是有人想知道暴政如何把人世间、人性里面最美好的事物摧毁，看看这几张照片就明白了。<br />
　　<br />
　　情深更莫醒<br />
　　<br />
　　戴乃迭是杨宪益三十年代在牛津大学求学时认识的，介绍他们认识的梅勒（Bernard Mellor）是杨宪益的同班好友，当时梅勒正在热烈地追求着戴乃迭。杨宪益被有着一头美丽的金发和碧蓝的大眼睛、对中国特别感兴趣的戴乃迭吸引。戴乃迭出身于传教士家庭，父母为了传教年纪轻轻就到了中国。戴乃迭出生在北京，部分童年是在北大度过的，七岁才返回英国，北京是她遥远而美丽的梦。戴的父亲是一个理想主义者，对中国一往情深，在燕京大学等学校教书十几年后，决定放弃大学教授职位与几个同道人去中国最落后的地方去帮助穷人，他的献身精神对戴乃迭影响很大。<br />
　　<br />
　　戴乃迭对中国有特殊的感情，后来更加决定放弃原来的法国文学专业而选择了中国文学；而在爱情的决择上，戴乃迭决意跟从杨宪益一起回到当时国破家亡的中国。当时杨宪益已经决定毕业后放弃到哈佛大学上研究生的机会，回到中国内地去，“直到中国从她的侵略者手中获得自由的那一天”（第86页）。梅勒因为失去戴乃迭伤心地哭过很多次，并与戴杨两位好朋友断了来往，在阔别四十年后才再度重逢。戴乃迭的母亲为了阻止她嫁给杨宪益到中国，曾经警告说“如果你嫁给一个中国人，你会后悔的。要是你有了孩子，他们会自杀的”（第406页）。就连戴乃迭的父亲也劝告她：“先别忙结婚，在中国住上几年后，如果觉得受的了那个苦，再结婚。” 结果戴乃迭还是义无反顾地跟从杨宪益几经颠簸周折，回到了饱经炮火摧残的中国。杨宪益在自传中说：“我离开牛津没有任何的遗憾，因为有Gladys陪我一起回去。”（第88页）<br />
　　<br />
　　他们哪里料到：戴乃迭母亲的预言，四十年后在他们深爱的那片土地上一一应验。戴乃迭母亲“文革”其间在英国去世时，戴乃迭正在受牢狱之苦，她母亲多方打听也不知道女儿死活，临终未能见上女儿最后一面。<br />
　　<br />
　　醒莫更深情<br />
　　<br />
　　戴乃迭用“一脚踏回到中世纪”来形容初到战乱中的内地的感受，连她结婚当天的头发也是杨宪益剪的。在接下来艰难的日子里，他们迎来了三个孩子的出生；杨宪益在四十年代被梁实秋邀请加入国立编译馆，用三年时间翻译了三十五卷《资治通鉴》；后来更成了没有共产党员身份的“地下工作者”。在政权更替时，国立编译馆上级部门教育部长杭立武专门为杨宪益一家留了四个飞往台湾的位置，但杨宪益却又一次选择留下来。杨宪益没有料到：这一决定让他在后来的三十多年里，“没有完成一件他完全有能力做完的工作”（P129）；而当时选择了离开的梁实秋，虽然在八年抗战中只翻译了一部莎士比亚戏剧，但后来在美国稳定的学术环境里完成了全部莎士比亚戏剧的翻译。<br />
　　<br />
　　雷音特别引用李锐把当时由于政权更替导致中国知识分子“走”与“留”的抉择称为“新文化运动的生死劫”。“正如李锐所说：‘面对着城外隆隆的炮声，所有对于主义的探讨、设想，所有对于思想的追问、争论，所有写在纸上的判断、推理，全都变得无足轻重，全都简化成一个迫在眉睫的生死决战――― 走，还是留下？’同是诞生于西方文化的影响和冲击下的两种主义―中国的三民主义和中国式的共产主义主义以及代表他们的两个政党及其知识分子分流了，他们中的一部分人从此浪迹天涯，致死也无法回到自己的父母之帮。而留在大陆的另一部分人则经历了脱胎换骨的生死磨难。”（第161页）。<br />
　　<br />
　　很不幸，杨戴两人成了这个经历“生死磨难”的群体的一份子，他们对共和国的付出，杨宪益两次向故宫博物院和南京博物馆无偿捐献自己收藏的珍贵文物的无私，以及戴乃迭不惜变卖订婚戒指等全部首饰以助杨宪益购买飞机支持抗美援朝所表现的忠诚，也无法改变他们在“文革”被打成“特嫌”的命运，受了四年牢狱的无妄之灾；而与他们划清了界线的三个子女则被发配三地，过着身无分文受人歧视的生活；杨宪益的七十多岁的母亲被罚每天扫大街；他们最珍爱、期望最高、天资聪颖的的儿子杨桦，因为母亲的外国人身份被拒于外公曾经执教的北京大学门外，带着红卫兵抄了自己家的“大义灭亲”表现也没有换得信任，因为长着一副外国人面孔而受尽迫害；杨桦对爱情的态度与在读书无禁区的年代成长的杨宪益有着天渊之别，觉得择偶的首要标准是“政治条件”,,,,,,<br />
　　<br />
　　戴乃迭是在出狱后，才明白什么叫“株连九族”。她到处打听三个子女的下落，她说要是在监狱里就知道家人的情况，恐怕早就撑不下去了。其实，戴乃迭被关押失踪后，英国学术界23位著名汉学家曾经联名写信给周恩来，要求知道戴乃迭的情况，可惜，请愿信最终石沉大海。等戴乃迭出狱时，已经与母亲阴阳相隔。更为滑稽的是：被关了四年的杨宪益，在出狱时还要出四年的“伙食费”。杨戴两人出狱后，希望为孩子们弥补一切，但已经太晚了。先是杨宪益患上精神分裂症，两年后才痊愈；然后是杨桦有一天突然宣布自己不是中国人，不承认杨宪益是他的父亲，开始只说英文不说中文，后来因为周恩来的过问，杨桦获批准到英国去，临走前他把所有的书都烧了，只留下了团员证和毛泽东像；虽然离开了中国，但身在英国的杨桦一看到中国人就害怕发抖，最后这位年轻人选择了在圣诞期间点燃汽油自焚结束了自己的生命。杨桦死前把毛泽东像戳满了洞，他的墓碑上刻的是他的英国名字David Green。<br />
　　<br />
　　书中提到杨桦宣布自己是英国人的举动的“灵感”来源于他的朋友卡玛，也就是后来拍六/四/纪录片“天/安/门”和文/革记录片“八九点中的太阳”的卡玛。杨桦的生命结束于“八九点中的太阳”，他就这么落下了，留下母亲戴乃迭独自去承受六/四的最后一击。<br />
　　<br />
　　至此，戴乃迭母亲当年的预言一一应验 C 她唯一的儿子自杀，大女儿伤心欲绝不愿意回到中国，他们的家庭四分五裂。历史跟杨宪益戴乃迭开了个大玩笑：杨宪益在三十年代选择离开牛津放弃哈佛，并且在四十年代选择留在中国，可以说是一个男人在事业和信仰这两大重要问题上下的最大赌注；而当初戴乃迭不顾一切跟随杨宪益回到中国，可以说是一个女人在感情和婚姻这两大问题上作的最重要决定。然而，与杨宪益生死相随的戴乃迭却在文革因为被囚禁而被迫与杨宪益分离；他们的爱子因为戴乃迭的外族身份受到迫害而决意逃离中国，最后选择作为一个英国人来结束自己的生命，而一直在伤害他们、背叛他们的不是当年杨宪益要赶走的“侵略者”，而是他们夫妇两人投入生命中的一切去信任、去支持的代表他们的理想的政权，和那些在权力背后的人。<br />
　　<br />
　　传记里提到杨宪益生命里作的两个重要决定的原因都是因为他觉得如果不那样做就会觉得“羞愧”：一次是回国支持抗日；另一次是公开谴责“六/四”屠/杀。对于一个理想主义者而言，还有什么比从情深到梦醒更叫人痛苦的呢？<br />
　　<br />
　　“青春作伴多成鬼 白首同归我负卿”<br />
　　<br />
　　戴乃迭与杨宪益的人生经历，可以是一部很好的英雄爱情片的剧本，但没有人愿意这样的情节真实地发生在自己身上。戴乃迭几十年来竭尽全力去维护自己的婚姻和家庭，但当她母亲离世时，她没有见到最后一面；当她的儿子孤苦无助地走向绝路时，她不在他的身边；坚强的她终于在杨宪益六/四/讲话的最后一击后倒下了，面对家散人亡的她不可以再失去杨宪益了。我想，那时候的戴乃迭是在爱与恐惧之间分裂着，正如杨宪益在良知与苦难中挣扎一样 -- 杨宪益一次又一次地选择为他信仰的理想和主义付出一切，而戴乃迭则选择为她深爱的男人付出一切，从这个意义上说，戴杨两人注定要成为那个打着“正义”旗号、漠视生命和人性的政权的牺牲品。在戴乃迭最后的日子里，杨宪益拒绝一切社交活动，寸步不离守护着她，并反复强调“我在牛津最值得说的是认识了戴乃迭― 我的爱人，” 希望可以挽回戴乃迭对生活的信心。他们当年热恋时一定也对未来有过憧憬，他们一定也为孩子们的第一声哭喊兴奋过，可惜，戴乃迭穷尽一生，还是无法保护自己的孩子；杨宪益一辈子光明磊落，却无法保护自己的爱人。戴乃迭离世后，杨宪益写下了“青春作伴多成鬼，白首同归我负卿。天若有情天亦老，从来银汗隔双星”的诗句。<br />
　　<br />
　　对于六/四/后失去记忆的戴乃迭来说，那是他们苦难的终结；而对于大批因为六/四/而改变生命轨迹的学生和知识分子来讲，那只是苦难的开始。在那个把生命里最美好的事物例如爱情亲情友情与恐惧苦难联结起来的政权统治下，一代又一代的妻子们孩子们就注定了要跟随良心未泯的丈夫们父亲们付出着沉重的无辜的代价。不知杨桦是否会在天国苦苦追问：谁有资格用“正义”的名义，牺牲我们的幸福？<br />
　　<br />
　　谨以此文献给我在小金丝胡同里见到的那位值得尊敬的老人 -- 杨宪益先生。</div>

							
							
							
							
