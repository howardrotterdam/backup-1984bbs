													<h2>为什么山东蓝翔高级技工学校强于哈佛大学？</h2>
						
						
																											<div id="postmessage_484897" class="t_msgfont">为什么山东蓝翔高级技工学校强于哈佛大学？<br />
冷推荐, 冷新文 | 易水寒 | 23 评论 | 星期三, 七月 15th, 2009 | 1591 浏览<br />
山东蓝翔高级技工学校强于哈佛大学，这即将是众所周知的事，我今天就来证明一下！<br />
<br />
１。山东蓝翔高级技工学校是我党领导下的射会主义国家公立大学，哈佛大学是没落的资本主义国家的私立大学。两种国家的性质决定了山东蓝翔高级技工学校地位必然高于哈佛大学，这是历史的进步，是谁也无法抹杀的。<br />
<br />
2.学校的占地规模决定了山东蓝翔高级技工学校强于哈佛大学，山东蓝翔高级技工学校占地面积1000亩。而哈佛大学总共占地才500英亩，小得可怜。<br />
<br />
3.学生人数上哈佛大学再次告负。目前，山东蓝翔高级技工学校现有全日制在校生14000人，成人教育学生6000多人，也就是一共两万。而哈佛大学才1.3万，相比山东蓝翔高级技工学校少了0.7万人。为什么会少了0.7万人？不正是说明世界人民心目中喜欢山东蓝翔高级技工学校的人多于喜欢哈佛大学的人吗？<br />
<br />
4.学生的素质。山东蓝翔高级技工学校的学生不但全部流利掌握中文，并且还通过了英语大学三级四级甚至六级的考试。而哈佛的学生除了会说英语，懂中文的人实在是少之又少。同样，作为学校教学和科研的主力军，山东蓝翔高级技工学校有很多老师既有中国的教育背景，又有国外的留学背景。而哈佛大学，全校竟找不出几个有完整中国教育背景的老师来。<br />
<br />
5.研究机构数量的差异决定了山东蓝翔高级技工学校强于哈佛大学。哈佛设有以下学院：文理学院，商学院，设计学院，神学院，政府学院，教育学院，法学院，医学院，牙医学院，公共学院，拉德克利夫学院（女子学院）和大学扩展部一共12个机构。<br />
再来看看山东蓝翔高级技工学校的研究机构和系所：设有厨师、汽修、挖掘机、美容美发、电气焊、无线电、计算机、缝纫等八大门类60多个专业。山东蓝翔高级技工学校是一所特色优势鲜明，理、工、经、管、文、教、法多学科协调发展的省政府省教委重点建设和培养的新型综合大学。在城市化发展这么快点今天，山东蓝翔高级技工学校毕业的学子将为全世界的城市建设做出自己的贡献。请问，如果不是实力雄厚，山东蓝翔高级技工学校敢设立这么多研究机构吗？<br />
<br />
6.两个学校不同的校训预示了两个学校未来的命运。山东蓝翔高级技工学校的校训是”输出一个青年,富裕一个家庭,带活一方经济”，山东蓝翔高级技工学校精神：我校围绕“高级”突出了特色，加强文理渗透，理工结合，实现了多学科协调发展；保持多年来“面向中小城市、面向基层”，“特别能吃苦、特别能忍耐、特别能工作”，“学得好、下得去、用得上、留得住”的优良传统。读起来琅琅上口，一种朝气蓬勃的社会主义优越感，而哈佛大学的校训是”以柏拉图为友,以亚里士多德为友,更要以真理为友”，听起来让人有同性恋的感觉，而且还有一种阴沉沉的感觉。<br />
7.另外结合本人的个人经历，我更深深体会到山东蓝翔高级技工学校作为一所世界级名校在所有学子心目中的崇高地位。我本人作为一名美貌与智慧并存，刚毅而不失温柔的有为青年，除了天资聪慧外，更是每日三更灯火五更鸡鸣勤奋苦读，经过不懈的努力，历经2次高考，终于有幸被山东蓝翔高级技工学校缝纫机科学系破格录取。大一的那一年我还记得每天夜幕降临的时候我们都在孜孜不倦的上晚自习。而哈佛大学，在本人的2次高考经历中，没有一次愿意填报这所学校。我们的报考书上更是不屑把这个学校算作一个值得报考的大学添加在书里。<br />
<br />
强弱悬殊 高下立判！<br />
<br />
综上所述，我可以自豪地说C－－”选择山东蓝翔高级技工学校，无怨无悔，因为它就是世界上最好的大学”。我们承认哈佛大学在世界上名气大于山东蓝翔高级技工学校，但这是由于美帝国主义的媒体掌握着话语权，有意压制山东蓝翔高级技工学校而造成的结果，我们相信通过全体校友在网络上的宣传，我们一定可以让世界人民认识并喜欢山东蓝翔高级技工学校。<br />
<br />
飞翔吧，山东蓝翔高级技工学校！<br />
