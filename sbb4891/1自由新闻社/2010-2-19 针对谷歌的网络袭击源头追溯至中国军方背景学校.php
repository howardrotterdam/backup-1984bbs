																											<div id="postmessage_484973" class="t_msgfont">蓝翔技校掌门人――荣兰祥的创业传奇<br />
<br />
没有存款的企业家少之又少，荣兰祥就是其中一个。他没有攒钱的习惯，每一分盈余很快会变成一粒沙、一块砖，为走进蓝翔技校的学子们预铺着前程。<br />
　　创业篇：借壳办学<br />
　　1984年，春潮涌动。不到20岁的荣兰祥按捺不住创业的冲动，决定到济南办所技校传授技术。当时个人无法拿到办学证件，荣兰祥琢磨好些时日，终于想到了与其他学校合作、借壳办学的办法。<br />
　　怀揣着几千元钱，荣兰祥用20多天时间跑遍了济南市区所有中小学，寻找可租房源、探寻合作伙伴。教育部门一位负责人得知后深受感动，“我从事教育20 多年都没转遍所有中小学，你却做到了。”这位负责人当即帮助荣兰祥牵线。<br />
　　1984年10月，天桥区宝华街一所十分简陋的院落内，一个小小的摩托车维修培训班――― 山东蓝翔职业培训学校的雏形――― 开班了。尽管一无资金、二无设备，房子也是租赁的，但荣兰祥还是异常欣慰，他迈上了鲜有人尝试的民办职教征途。【成长之道】万事开头难，在当时不靠国家投资的情况下尝试民办职教，创业之路更艰难。荣兰祥坚持认为，改革开放大潮已起，社会需要各类技能人才，办技校是利国利民、大有发展前途的事业，再苦再难也要坚持。<br />
　　荣兰祥：创业是一种信念，其动力来自对远大理想的追求；创业还是一种境界，需要智慧作支撑。创业有成败但没有早晚，成功贵在坚持不懈。<br />
　　崛起篇：机遇猎手<br />
　　1988年，部队掀起搞三产的热潮，荣兰祥果断抓住机遇，利用部队这一平台创建山东蓝翔职业技能培训学校。2000年，部队搞经营受到限制，羽翼渐丰的荣兰祥果断在药山脚下买地建校，开始单打独斗。2002年蓝翔技校占地扩大到50余亩，已达到年培训3万人的规模。不过这还不足以支撑荣兰祥建一流技校的“野心”，他随后咬牙再次大投入建设占地150亩的高级技工学校校区。该校区建成的2005年，<br />
　　培养的高级技工即供不应求，他又下决心上马了占地500余亩共20余万平方米建筑的新校区，培养档次瞄准工科高职学历教育。<br />
　　荣兰祥早就摸透了用工企业的心态，围绕市场抓就业，只培训受市场欢迎的工种。荣兰祥从来不攒钱，只要学校有一定盈余，马上会“招兵买马”、上马新的专业或设备。每次机遇来的时候，荣兰祥都不错过，因为他是一个时刻有准备的人。<br />
　　从简陋小院，到占地近千亩、30余幢楼共40余万平方米校舍；从10来名学员，到年培训能力3万人的规模；从提供短期技能培训，到高级技工、预备技师学制教育皆有；从单一工种培训，到数控加工、计算机等八大工种60余个专业，蓝翔技校正迅速崛起为全国一流技校。<br />
　　荣兰祥喜欢将远大理想分解成一个个短期目标，并为抓住机遇做好各种准备。每当完成一个目标、抓住一次机遇，他就不断提醒自己：每一步成功都是新的开始。这种“不知足”的劲头，让蓝翔技校逐渐牛气起来。全国知名技校很多，但敢向用人单位收费的可能就蓝翔一家，谁来要人先要交600元至1200元不等的预订费。对于社会上对蓝翔技校和其本人的一些争议，荣兰祥一笑置之，“总有一天，我要用事实证明一切。”<br />
　　荣兰祥：人既要做眼界深远的“望远镜”，为人生设定长远轨道，又要做洞悉眼前问题的“显微镜”，时刻纠偏、举一反三。知足常乐固然重要，但更要具备对现状不满的勇气、改变现状的谋略。<br />
　　回报篇：成己为人<br />
　　蓝翔技校每天都有变化，唯一不变的就是其办学宗旨―――发展职业教育，提高劳动者素质，为经济建设服务。蓝翔技校为社会培养人才，让学员成为人才，不让一名不合格学员走向社会。<br />
　　北京奥体中心不久前从蓝翔技校烹饪专业选拔了400名学生，专为奥运餐饮服务。该中心负责人是在考察北京一家五星级酒店后决定去蓝翔技校看看的，这家酒店有几位从蓝翔技校毕业的学员，都是顶梁柱式的员工。在蓝翔技校，数控机床专业学员生产的零部件直供中国重汽、济南二机床等厂家使用，挖掘机专业学员在校期间就能揽下一大堆活，厨师专业学员动手做的菜品数量是大学相应专业本科生的20倍，汽车维修专业学员经常拆装宝马、奔驰、奥迪等名车。荣兰祥说，学校如同企业，学生就是技校的产品。有质量才有学生，有学生才有发展。<br />
　　蓝翔技校去年纳税近1500万元，今年有望突破2000万元，目前已为社会培养各类人才30多万。蓝翔的学生中，百万富翁多不可数，千万富豪亦不鲜见。在保证学校继续发展的同时，学校以各种方式积极回报社会，近3年来免费培训并安排就业农民、下岗职工、特困青年等近3000人，为驻地经济和环保建设投入数百万元。荣兰祥还援建桥梁、帮扶贫困村、参加慈善捐助，兑现着“做一个有社会责任感的人”的承诺。<br />
　　四川汶川特大地震发生后，荣兰祥以个人名义捐款50万元，学校师生爱心捐款近6万元。荣兰祥还积极响应市政府号召，决定出资近700万元，免费为四川灾区培养200名高级技工，同时从学校工程机械专业抽调25台挖掘机、4台装载机、1台压路机和103名技术骨干及带队人员，组成了抗震救灾应急分队，第一时间上报相关部门，随时准备开赴四川灾区。【成长之道】正所谓“授人以鱼不如授人以渔”，荣兰祥最大的贡献不在于捐了多少款、向国家缴了多少税，而在于为社会培养了30多万人才，教给了他们谋生发展的技能。即使没有进行过慈善捐助，荣兰祥仍然可以在社会贡献榜上留下名字，上榜理由―――输出一个青年，富裕一个家庭，带活一方经济。<br />
　　荣兰祥：一个优秀的企业必然有着强烈的社会责任感，必然会以力服人、以理服人、以德服人。可以说，成人与成己是相辅相成的，壮大自己是为了成就别人，成就别人也就兼达自己。企业与社会、与人文需要、与周围环境的协调发展，才是当今社会最为需要的。</div>

							
							
							
							
