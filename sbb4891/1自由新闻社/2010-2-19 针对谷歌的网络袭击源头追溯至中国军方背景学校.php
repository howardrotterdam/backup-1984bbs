																											<div id="postmessage_485189" class="t_msgfont">纽约时报：两所中国学校被指参与了网络攻击<br />
作者：xiaomi2020<br />
有 6 人喜欢此条目<br />
原文：NYT：Two Chinese Schools Said to Be Tied to Online Attacks<br />
<br />
译文：纽约时报：两所中国学校被指参与了网络攻击<br />
<br />
一系列针对谷歌和其他美国公司的网络袭击的源头已经被锁定在了两所在中国的教育机构，上海交通大学和山东蓝翔技术学校。<br />
<br />
作者：JOHN MARKOFF and DAVID BARBOZA<br />
发表时间：2010/02/18<br />
译者：@foodpizza等译者合作完成<br />
<br />
<br />
<br />
图：Daniel Rosenbaum 为纽约时报拍摄<br />
James C. Mulvenon 说中国政府经常使用“红客”志愿者来执行策略。<br />
<br />
旧金山――据参与调查的人员透露，一系列针对谷歌和其他美国公司的网络袭击的源头已经被锁定在了两所在中国的教育机构，其中一所与中国军方联系十分紧密。<br />
<br />
他们同时也透露这次的旨在窃取商业机密、电脑源码和获取中国人权运动者的电邮的袭击可能早在4月份就已经开始了，这大大早于先前的猜想。谷歌在1月12日宣称说它自己和其他一些公司已经成为可能源自中国复杂的网络袭击的目标。<br />
<br />
包括国家安全局（NSA）在内的电脑安全专家自那以后就开始要准确定位这次攻击的源头。直到不久前，调查只找到了几个来自台湾地区的服务器。<br />
<br />
如果根据后期的调查来看，发现的问题与得到的答案一样多，其中包括这种可能性，网络袭击来自中国，但并不一定是来自官方，甚至来源有可能不在中国地区。<br />
<br />
更进一步的追溯指向了一所中国重点大学和一所职业学校，成为此次艰难任务的一个重大突破。从面临同谷歌一样受到攻击的美国军方合作伙伴获得的证据显示一个有攻击嫌疑的链接指向了一个特定的由一位乌克兰教授在这所职业学校教授的电脑班。<br />
<br />
这一进展是在一次电脑安全专家会议上由这位合作方提供。<br />
<br />
涉及到此次攻击的是上海交通大学和蓝翔职业技术学校，据几位参与调查的专家匿名透露，他们没有授权与外界讨论这次调查。<br />
<br />
上海交大有着中国顶级的计算机科学项目。就在几个星期之前，来自交大的学生刚刚赢得了一个由IBM举办的国际计算机程序比赛的奖项――这场“智力的比赛”中――他们打败了来自斯坦福和其他几所最顶尖的高校的学生。<br />
<br />
蓝翔技校位于中国东部的省份山东，它是一所由军方支持建设，为军队培养了一些计算机科学家的大型技校。这所学校的网络是由与百度有着紧密的联系的公司来运营的――它是中国搜索引擎的巨头，也是谷歌的竞争者。<br />
<br />
来自电脑安全行业和奥巴马政府的分析家们在如何解释袭击源头来自学校而不是来自中国军方或是政府机构上产生了分歧。一些分析家已经私下的流传着一份文件说这个职业学校就是政府机构的一个幌子。但是其他的IT界高管和前政府官员声称这所学校也有可能是由第三国情报机构运行的“假象”。还有人也认为这次黑客袭击可能是一个巨大的行业窃密犯罪案例，旨在窃取美国科技公司的知识产权。<br />
<br />
监控中国信息战争的独立研究者警告说中国已经采用了某种高度分散的方法来进行网络窃密，这使得想要找到网络攻击的源头是几乎不可能。<br />
<br />
“我们得明白，他们有着完全不同的电脑网络袭击模式，”一位华盛顿研究中国军事的专家，同时也是情报研究与分析中心的主任，James C. Mulvenon说。不同于美国有着专门分工的网络攻击机构，他说，中国政府经常找“红客”当志愿者来执行当局的政策。<br />
<br />
 这两所中国学校的发言人说他们并没有听说美国调查机构已经把攻击谷歌的源头追踪到了他们的校园内。<br />
<br />
“如果这是真的，我们会向相关部门报警，并展开自己的调查，”上海交通大学党委宣传部负责人刘玉祥（音）说到。<br />
<br />
但是当被问及到发动攻击的可能性的时候，交大信息安全工程的一位领先的教授在一次电话采访里说：“我并不感到惊讶。事实上学生们对国外网站进行黑客攻击是家常便饭。”这位教网络安全的教授因为害怕报复而要求不要透露自己的姓名。<br />
<br />
“我相信有两种情况，”教授说。“一种是完全个人化的错误行为：一两个调皮捣蛋的学生就是热衷于实践从学校学来的骇客知识，考虑到学校内部的资源和网络都受限制。或者是另一种常发生的情况：大学的IP地址为其他人所劫持。”<br />
<br />
蓝翔职业技术学校的领导说，他们没有听说与网络攻击事件有任何关联，拒绝表态是否有乌克兰教授在学校教授电脑课。<br />
<br />
一位姓Shao的男士，他是该学校计算机科学系系主任但拒绝透露全名，说，“我认为我们的学生不可能攻击谷歌或是其他美国公司，他们只是高中毕业生，没那么高水平。而且，我们学校实行封闭式管理，外来人员不可能轻易进入学校。”<br />
<br />
 Shao先生承认，每年计算机科学系中有四、五名学生会应征入伍。<br />
<br />
谷歌走到台前挑战中国的决定，为美国政府制造了一个高度敏感的议题。在其宣布受到攻击之后不久，国务卿希拉里・克林顿发表了一场关于互联网审查的演讲，挑战了中国，暗示中国政府控制互联网上的自由信息获取实际上是在建造一堵信息时代的柏林墙。<br />
<br />
 2009年10月Northrup Grumman就网络战问题向美中经济安全审查委员提交了一份报告，其中指出中国军方在六个区域部署了军事力量，涉入到类似的攻击中。蓝翔职业技术学校所在的济南是其中一个。<br />
<br />
谷歌的高官没有提及入侵事件，也不会就本文发表评论。但公司已联系了电脑安全专家确认了其他受攻击公司所做的同样报告：入侵者利用之前微软公司的IE浏览器的一个未知漏洞，获取了公司服务器的访问权。<br />
<br />
法学检验分析正在揭开新的细节，入侵者如何利用这个漏洞进入到企业内部服务器中。他们通过一项聪明的技术，所谓的“信箱人”――利用在组织中协同工作的人们对合作者的信任做到这一点。<br />
<br />
侵入了一台电脑之后，入侵者在电子邮件中插入一个包含携带恶意软件的电子附件，它很可能会由第二个受害者打开。附件中的恶意软件就有可能让入侵者接管目标计算机。<br />
