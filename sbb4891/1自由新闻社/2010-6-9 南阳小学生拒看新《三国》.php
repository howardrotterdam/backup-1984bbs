													<h2>再挑孔明躬耕地之争 南阳小学生拒看新《三国》</h2>
						
						
																											<div id="postmessage_669341" class="t_msgfont">5月29 日，南阳30名小学生在卧龙岗前齐诵《出师表》，抗议新《三国》。<br />
<br />
　　关于诸葛亮躬耕地，襄阳与南阳已经争了上千年。而新近热播的电视剧新《三国》又在这个问题上点了一把火，因将躬耕地“搬”到了襄阳而引起南阳的强烈不满。日前，著名作家二月河、周同宾，南阳诸葛亮研究会副会长张晓刚等人就此话题接受了记者的采访。<br />
<br />
　　新《三国》“偏向”襄阳<br />
<br />
　　“正在热播的电视剧新《三国》，引起了诸葛亮躬耕地――南阳百姓的热切关注。可是最近，南阳人却越看越觉得不对劲儿，特别是新《三国》第32集里的一场戏，把躬耕于南阳的诸葛亮说成是居住襄阳隆中，把卧龙先生隐居的南阳卧龙岗也搬迁到了襄阳。”6月6日，南阳诸葛亮研究会副会长张晓刚向记者述说了他的不解和愤怒，“简直是胡编乱造，新《三国》咋能这样不严肃呢？”<br />
<br />
　　记者注意到，新《三国》第32集有一场戏，徐庶向刘备举荐诸葛亮时说：“襄阳城外三十里，有一片山野名叫隆中，住着一位当代奇才……此人姓诸葛，字孔明，因为住于卧龙岗上，所以又号卧龙先生。”<br />
<br />
　　张晓刚说：“新《三国》依据的是罗贯中的《三国演义》，这场戏在《三国演义》里属于第三十六回，即《玄德用计袭樊城 元直走马荐诸葛》。罗贯中在此处已明确提到了诸葛亮与襄阳、南阳之间的关系。”<br />
<br />
　　《三国演义》中，徐庶对刘备推荐诸葛亮时说：“此人乃琅琊阳都人，复姓诸葛，名亮，字孔明，乃汉司隶校尉诸葛丰之后。其父名，字子贡，为泰山郡丞，早卒；亮从其叔玄。玄与荆州刘景升有旧，因往依之，遂家于襄阳。后玄卒，亮与弟诸葛均躬耕于南阳。尝好为《梁父吟》。所居之地有一岗，名卧龙岗，因自号为卧龙先生。此人乃绝代奇才，使君急宜枉驾见之。若此人肯相辅佐，何愁天下不定乎！”<br />
<br />
　　《三国演义》还写了刘备的反应：“引众将回至新野，便具厚币，同关、张前去南阳请孔明。”同时，还写了后人赞徐庶走马荐诸葛所写的一首诗：“痛恨高贤不再逢，临岐泣别两情浓。片言却似春雷震，能使南阳起卧龙。”<br />
<br />
　　张晓刚认为，客观地讲，《三国演义》既写了诸葛亮曾经居住于襄阳，又写了诸葛亮躬耕于南阳卧龙岗。写了徐庶与刘备道别于南阳新野之后，经过南阳卧龙岗见到诸葛亮之后，又继续前行至许昌。而这样，也符合由新野去许昌时途经南阳这一地理实际。因此，新《三国》在此不提诸葛亮躬耕南阳，并将诸葛亮躬耕地――南阳卧龙岗硬搬到了湖北襄阳，违背了原著。张晓刚还说，根据历史记载，三国时，襄阳是一个很小的县，当时更没有隆中这个地名。隆中当时叫“阿头山”，新《三国》中徐庶说襄阳城外三十里，有一片山野名叫隆中，简直成了一句极其“穿帮”的笑话。<br />
<br />
　　南阳网友卧龙岗前砸掉电视机<br />
<br />
　　新《三国》将诸葛亮躬耕地搬到襄阳的剧情，引起了南阳人的强烈不满。电视剧播出后，不断有网友和当地学生到南阳卧龙岗武侯祠前聚会，他们一起诵读“臣本布衣，躬耕于南阳……”，一起声讨新《三国》篡改历史。<br />
<br />
　　5月29日上午8点，南阳数百网友还在卧龙岗山门前的“千古人龙”牌坊下，拉起条幅，30名小学生齐声诵读前《出师表》，游人与市民签名声援。活动的最后，几名网友还抬出一台电视机当场砸毁，以示对新《三国》的抗议。<br />
<br />
　　参与活动的网民“二月公子”对记者说，诸葛亮躬耕地就在南阳，毋庸置疑，现在网友们反对的是别有用心的人利用话题“编造历史”，以争议获取收视率和经济效益。<br />
<br />
　　“二月公子”说，诸葛亮所撰前《出师表》、《黄陵庙记》中一再自述“躬耕于南阳”、“躬耕南阳之庙，遂蒙刘氏顾草庐”，民间也自古流传“南阳诸葛亮，稳坐中军帐”、“南阳有个诸葛亮”、“三顾茅庐”等谚语和成语，清晰表明诸葛亮躬耕地在今河南南阳，躬耕地有“草庐”。<br />
<br />
　　许多网民表示，保卫“诸葛亮躬耕地”并非意气之争，而是正本清源、捍卫历史真相的行动，是避免伪学说贻害后人的举动。“奉劝一些文化商人和投机贩子，不要为了利益出卖祖宗，做历史的罪人。”<br />
<br />
　　网民的行动也遭到了外地网民的质疑：“名人故里争就争了，为何还拉上小学生当道具？”因为，外地一些网民看到，南阳一些小学生也去签名“抗议”，参加到了抗议新《三国》的行列。<br />
<br />
　　对此，网民“卧龙达人”称：“导演和编剧用这种‘烂片’误导我们的下一代，你说和孩子无关吗？南阳是诸葛躬耕地，所以我们不需要和谁争。我们只是在教育我们的孩子要从小学历史，明是非，诚信做人，懂得尊重历史！”<br />
<br />
　　一位网民还提出了自己的担忧，他发帖说：“南阳不是要投巨资打造卧龙岗文化吗？现在播出的新《三国》会向公众传达错误的信息，这将给南阳的卧龙岗改造和文化建设造成巨大的冲击！如果外地人都认为三顾茅庐发生在襄樊，那我们的卧龙岗开发还有什么意义？”<br />
<br />
　　还有网民发帖，要求“高希希回家学历史”、“高希希和朱苏进来南阳卧龙岗为历史谢罪”。高希希，系新《三国》导演，朱苏进为编剧。<br />
<br />
　　网上调查三地之争南阳领先<br />
<br />
　　5月18日，某网刊登文章《诸葛亮故里之争：襄樊南阳上下PK数百年》。文章称：“围绕诸葛亮故里、故居、躬耕地的现代经济故事的演绎，也如八阵图一般，扑朔迷离，异象环生，具体涉及河南南阳、湖北襄樊(古襄阳所在地)和山东临沂三地。而且，此乃名人故里之争中唯一一桩持续数百年的历史旧案，从大清国一直到现在，三地越争越复杂微妙，旁观者越看越疑惑丛生。”<br />
<br />
　　紧跟文章的是网上的一项调查：您认可的诸葛亮故里是在哪座城市？<br />
<br />
　　5月19日，该网刊发调查进度一文《过半网友认为河南南阳才是诸葛亮故里 远超襄樊和临沂》，称截至5月19日17时33分，共有9969份投票，58%的网友选择了现今的河南南阳是诸葛亮故里，票数远远超过湖北襄樊。<br />
<br />
　　诸葛亮故里之争由来已久，有关资料记载，可以追溯到清道光年间，前后已达数百年。而现今参与诸葛亮故里争夺的不仅仅是南阳和襄樊，山东临沂也称是诸葛亮故里。截至6月6日17时30分，记者查看了这则调查，南阳排名仍为第一，约21229票，占63.7%；襄樊8866票，占26.6%。<br />
<br />
　　张晓刚说，这一调查把新《三国》挑起的新一轮诸葛亮躬耕地之争推向了高潮，但是不知道笔误还是缺乏这方面的知识，调查关于“故里”的提法还是有点欠妥，准确地说，诸葛亮故里没有争论，即山东琅琊，南阳和襄樊所争的是诸葛亮躬耕地，但无论怎么说，诸葛亮的话“臣本布衣，躬耕于南阳”已经深深印在人们的脑海里。<br />
<br />
　　二月河：大家不用相信新《三国》<br />
<br />
　　“新《三国》的编剧认为诸葛亮躬耕地在襄阳，学术界也不会把他表达的东西当成考证，大家也可以不信的。”著名作家二月河接受记者采访时说，诸葛亮躬耕地就在南阳。南阳在汉代是大都市，有《南都赋》为证，它在当时是除首都之外的第二大城市，经济、文化、教育等都极为昌盛。张衡、张仲景等名垂千古的优秀人才也都是和诸葛亮同时期的人。而襄阳在当时很小，出现诸葛亮的情况几乎是不可能的。这就好比清华大学、北京大学出现了一拨顶尖人才，我们可以理解；但突然在穷乡僻壤里冒出一个诸葛亮，那就不可思议了。《出师表》中，诸葛亮自己讲“臣本布衣，躬耕于南阳”，过了一千多年，现在说诸葛亮不是在南阳躬耕，这是没有依据的。<br />
<br />
　　对于诸葛亮躬耕地之争，二月河一直保持着自己的观点。他曾在多个场合发表言论说：“历史不是一团泥巴，谁想捏个啥样是啥样；历史也不属于有钱人，历史是属于人民的。”二月河相信，真理总能越辩越明，他对某些地方的做法很不以为然，认为是“很滑稽的事”。<br />
<br />
　　南阳有关方面 要求新《三国》道歉<br />
<br />
　　著名作家、全国首届鲁迅文学奖获得者周同宾认为：“电视剧剧情是可以虚构的，但在整体上不能违背史实。”周同宾说，诸葛亮躬耕地在南阳众人皆知，躬耕地和故居地是两个概念，只要是住过的地方，有房子的都可以称为故居地。诸葛亮在山东，河南叶县、唐河桐寨铺等地都住过，也可能在襄阳住过。因此，襄阳是诸葛亮故居地的说法是可行的，但是故居地并非躬耕地。<br />
<br />
　　周同宾说，《出师表》就可以说明一切了。“躬耕地”并不一定是下田种庄稼，有可能是隐居的意思，但更主要是研讨学问，关心天下形势。<br />
<br />
　　南阳师院汉文化研究中心主任郑先兴说，诸葛亮躬耕于南阳，不是根据历史上或现当代哪位学者的论断，而是根据诸葛亮的自述“臣本布衣，躬耕于南阳”。<br />
<br />
　　西晋陈寿在《三国志》中叙述：“亮早孤，从父玄为袁术所署豫章太守，玄将亮及亮弟均之官……玄卒，亮躬耕陇亩，好为《梁父吟》。”陈寿并没有说诸葛亮是躬耕于南阳还是襄阳，因为诸葛亮的躬耕地是不需要争论的问题。<br />
<br />
　　郑先兴说，最先说诸葛亮躬耕于襄阳的人是东晋襄阳人习凿齿，他在《汉晋春秋》里说：“亮家于南阳之邓县，在襄阳城西二十里，号曰隆中。”这一说法，可谓是穿凿附会。因为在两汉、三国时期，南阳郡与南郡以汉水为界，南阳属南阳郡，而襄阳属南郡，习凿齿为了使资料记载合乎自己的想法，干脆将襄阳“下嫁”，说襄阳曾属南阳郡的邓县管辖。“史料上没有一字说诸葛亮的躬耕地在襄阳。”郑先兴说。<br />
<br />
　　张晓刚说，南阳卧龙岗历史文化厚重，从各个层面证实了诸葛亮躬耕于南阳。首先，武侯祠的始建年代可上溯至魏晋时期，到唐宋年间，李白《南都行》云：“谁识卧龙客，长吟愁鬓斑。”刘禹锡《陋室铭》赞美：“南阳诸葛庐，西蜀子云亭，孔子云‘何陋之有’。”这些都表明南阳卧龙岗在唐宋时期已成为一处著名的人文景观。<br />
<br />
　　新《三国》何以如此？张晓刚分析认为，新《三国》播出时，襄樊在央视力推其早已录制的宣传片，再结合几年前发生的襄樊人运作更改教科书事件推测，襄樊与新《三国》剧组幕后应该有着某种密切联系。<br />
<br />
　　张晓刚说，南阳诸葛亮研究会将新《三国》的行为视为一种掠夺，将诸葛亮、卧龙岗从南阳夺走，其实就是掠夺了南阳的文化资源和人文资源。新《三国》的导演、编剧应对此事做出回应，及时改正错误做法，向南阳及所有被误导的观众致歉、谢罪。否则，南阳诸葛亮研究会将采取必要的手段，甚至诉诸法律来维护南阳历史文化资源、人文资源的合法权益。<br />
<br />
　　链接：诸葛躬耕地争夺史<br />
<br />
　　明嘉靖年间，当时卧龙岗上香火旺盛，也正是诸葛亮躬耕地争论激烈之际，襄阳说和南阳说两派各执己见，互不相让，官司打到翰林院，甚至惊动了明世宗。<br />
<br />
　　清道光年间，籍属湖北宜昌的顾嘉蘅到南阳就任知府，其时，襄阳人和南阳人打官司争抢诸葛亮，顾知府情急之下写了一副对联――“心在朝廷，原无论先主后主；名高天下，何必辨襄阳南阳”，置于武侯祠。<br />
<br />
　　1990年，中国邮政发行《三国演义》特种邮票第二组，其中有一张是“三顾茅庐”，又触动了襄樊和南阳两地的神经。最终，国家邮票发行部门不得不让两地各自举办首发式。<br />
<br />
　　2003年，人教版初中语文教材重新收录《隆中对》，其中加注称：“隆中，山名，在现在的湖北襄樊。”在《出师表》中把“南阳”注解为：“南阳，郡名，在现在的湖北襄阳一带。”此举强烈刺激了南阳上下，“教材门”爆发。结果，人教社给南阳道歉，修改了错误的教科书。<br />
<br />
　　2008年6月8日，央视某栏目插播了一则湖北襄樊的城市广告――“诸葛躬耕地，山水襄樊城”，又激起了南阳人的抗议。广告为此改为“诸葛故居地，山水襄樊城”，是为“广告门”。<br />
