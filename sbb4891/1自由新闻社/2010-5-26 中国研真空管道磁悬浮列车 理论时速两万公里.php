													<h2>中国研发时速4000公里真空管道磁悬浮列车 达第一宇宙速度</h2>
						
						
																											<div id="postmessage_642982" class="t_msgfont">2010-05-25 09:54:18　来源: 解放网-新闻晨报 ()　跟贴 1970 条 手机看股票<br />
<br />
<a href="http://money.163.com/10/0525/09/67H724UO002526O5.html" target="_blank">http://money.163.com/10/0525/09/67H724UO002526O5.html</a><br />
<br />
<br />
目前，人类采用的高速远程客运工具以飞机为主，民航客机的运营速度约为每小时1000公里。对于5000公里以上的远程旅行来说，乘飞机旅行耗费的时间、经济成本惊人，并且造成了严重的环境污染。今年爆发的冰岛火山灰危机，以及连续不断的空难事件，更让人们意识到了民航系统的缺陷。<br />
<br />
然而，部分中美科学家在接受晨报记者专访时透露，一种最低时速4000公里、能耗不到民航客机1/10、噪音和废气污染及事故率接近于零的新型交通工具――真空管道磁悬浮列车已呼之欲出。作为新一代磁悬浮列车，真空管道磁悬浮列车将把北京与华盛顿纳入两小时交通圈，用数小时完成环球旅行已经成为科学家近期努力的目标。<br />
<br />
中国在此项研究中已经走在世界前列，2007年，该项目被列为国家自然科学基金项目，由张耀平教授等专家申请的大量相关专利已被受理，一场交通运输革命已经迫在眉睫。<br />
<br />
新磁悬浮不惧环境影响<br />
<br />
晨报记者从有关部门获悉，2004年12月29日，一场有八名“两院”（中国科学院、中国工程院）院士参与、多名国内权威专家出席的研讨会在四川成都低调召开。众多学界权威参与此次会议的主要议题是――真空管道高速交通。简而言之，就是建造一条与外部空气隔绝的管道，将管内抽为真空后，在其中运行磁悬浮列车等交通工具，由于没有空气摩擦的阻碍，列车将运行至令人瞠目结舌的高速，大大缩短地球表面任意地点间的时空阻隔。管道由于是密封的，因此可以在海底及气候恶劣地区运行而不受任何影响。<br />
<br />
身兼“两院”院士殊荣的沈志云在会上指出，任何一种地面交通工具，不管是否悬浮，商业运营速度都不宜超过每小时400公里，否则能耗大、噪音超标，难以被市场接受，这是由稠密大气层决定的。但超高速是21世纪地面高速交通的需求，真空（或低压）管道式地面交通达到超高速的唯一途径，真空管道将是不可回避的选择。<br />
<br />
沈志云提出，我国应将目标定位在发展每小时600-1000公里超高速地面交通，分四个阶段推行，2020-2030年实现运营。<br />
<br />
中国科学院院士何祚庥虽然未参加此次会议，但他向大会提交了他的书面意见。他认为，在石油能源高度紧张的情况下，开展超高速磁悬浮列车技术的研究更具特殊意义。他通过技术分析指出，超高速磁悬浮列车的研发，在真空度问题上并不会存在原则性困难。<br />
<br />
理论上可达2万公里时速<br />
<br />
晨报记者获悉，首先将真空管道磁悬浮概念引进中国的科学家，是毕业于西南交通大学的张耀平，在2007年成功申请国家自然科学基金项目“真空管道高速磁浮交通基 础 研 究 ”（项 目 编 号 ：50678152）后，他的研究得到了政府层面的资助。在陕西省有关方面支持下，他目前调至该省西京学院，专门组建了真空管道运输研究所，正全力推进这一“运输革命”进入现实。<br />
<br />
张耀平在接受晨报记者专访时介绍，最早提出真空管道磁悬浮运输概念的，是美国兰德咨询公司和麻省理工学院的专家，真正将这一运输方式落实为图纸的，是美国佛罗里达州机械工程师戴睿・奥斯特（Daryl Oster），经过多年的研究与设计，戴睿于1999 年在美申请获得真空管道运输（ETT）系统发明专利。<br />
<br />
2001年，与戴睿相识并成为密友的张耀平将这项技术首次引进中国。2002年，戴睿和妻子前往中国，帮助张耀平和同事在西南交通大学组建了专门研究机构。经过多年努力，张耀平的研究获得了中国学界和政府全方位的支持，他认为，目前这项技术所需的技术已经完全成熟。“院士大会上专家们提出的每小时600-1000公里时速，是一个保守的对外口径，实际上所有研究者一开始就把这一运输方式的常规运行速度定位为每小时4000公里，经过技术改进，每小时6500公里是一个中期目标。虽现在不宜提得太高，但只要磁悬浮列车改进之后，克服技术障碍，那就相当于一颗卫星。近日我在与一名长江学者及其研究生座谈时，他们提出，真空管道磁悬浮列车的理路极限速度接近第一宇宙速度，要达到每小时2万公里是可以实现的。”<br />
<br />
张耀平在接受采访时指出，中国目前在各国研究者中研究进度最快。如果国家能将这项技术上升到三峡工程、长征火箭这样的国家高度，统筹资金、技术，就能在21世纪运输革命中占得先机。<br />
<br />
带来交通运输划时代变革<br />
<br />
戴睿和张耀平在接受晨报记者采访时指出，真空管道磁悬浮技术的意义，类似于当初蒸汽机取代马力，将带来划时代的变革。民航、铁路运输将被大面积取代，人类将进入更清洁、高效的旅行时代。<br />
<br />
张耀平介绍，近年来自己的团队已经连续申请了许多专利，已经克服了真空管道在建设中的实际障碍。“例如，管道与管道之间的接头处，必须密封严实。另外，管道沿线有许多抽气泵站，还要为维修、检查以及紧急情况预留能打开的开口，在真空管道运输系统正常工作时，这些开口都密闭，必须保证不漏气。在沿线各车站车辆进出主管道的空气锁部位， 系统连续运行时少量漏气不可避免，但闭合时的密封一定要可靠，达到相应的密封要求。管道中是真空状态，而在其中运行的磁浮车辆中必须是适宜人乘坐的大气环境，因此车辆必须具有良好的密封。”<br />
<br />
为了解决建造和运行中的难题，张耀平和他的团队日以继夜地工作，“真空管道中的隔离室”、“一种真空管道运输系统中磁悬浮车与车站间的对接装置”、“一种用于真空管道系统中的密封门”、“真空管道高速交通运行抽气系统”等专利相继问世。<br />
<br />
张耀平认为，目前科研项目唯一的需要，就是要从国家层面统筹建造第一条实验线路，“要有建设三峡大坝和开发大飞机、长征火箭项目的魄力，这个项目无法由某一高校、公司或单位单独完成，但一旦完成，中国将在技术革命中占据主导地位。”<br />
<br />
(本文来源：解放网-新闻晨报 () 作者：郭翔鹤 张冰玉 杨滟湫)</div>

							
							
							
							
