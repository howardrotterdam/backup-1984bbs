													<h2>中国研真空管道磁悬浮列车 理论时速两万公里</h2>
						
						
																											<div id="postmessage_639205" class="t_msgfont"><strong>北京与华盛顿将纳入两小时交通圈</strong><br />
<br />
<strong>&nbsp; &nbsp; 时速4000公里 我国研发真空管道磁悬浮列车</strong><br />
<br />
&nbsp; &nbsp; 目前，人类采用的高速远程客运工具以飞机为主，民航客机的运营速度约为每小时1000公里。对于5000公里以上的远程旅行来说，乘飞机旅行耗费的时间、经济成本惊人，并且造成了严重的环境污染。<br />
<br />
&nbsp; &nbsp; 不过，部分中美科学家在接受记者专访时透露，一种最低时速4000公里、能耗不到民航客机1/10、噪音和废气污染及事故率接近于零的新型交通工具――真空管道磁悬浮列车已呼之欲出――真空管道磁悬浮列车，将把北京与华盛顿纳入两小时交通圈，用数小时完成环球旅行已经成为科学家近期努力的目标，而中国在此项研究中已经走在世界前列。<br />
<br />
&nbsp; &nbsp;<strong> 新磁悬浮 不惧环境影响</strong><br />
<br />
&nbsp; &nbsp; 记者从有关部门获悉，2004年12月29日，一场有8名“两院”（中国科学院、中国工程院）院士参与、多名国内权威专家出席的研讨会召开。众多学界权威参与此次会议的主要议题是――真空管道高速交通。简而言之，就是建造一条与外部空气隔绝的管道，将管内抽为真空后，在其中运行磁悬浮列车等交通工具，由于没有空气摩擦的阻碍，列车运行速度令人瞠目结舌，可大大缩短地球表面任意地点间的时空阻隔。管道由于是密封的，因此可以在海底及气候恶劣地区运行而不受任何影响。<br />
<br />
&nbsp; &nbsp; 身兼“两院”院士的沈志云在会上指出，我国应将目标定位在发展每小时600-1000公里超高速地面交通，分四个阶段推行，2020-2030年实现运营。<br />
<br />
&nbsp; &nbsp; 中国科学院院士何祚庥虽然未参加此次会议，但他向大会提交了他的书面意见。他认为，超高速磁悬浮列车的研发，在真空度问题上并不会存在原则性困难。<br />
<br />
&nbsp; &nbsp; 相关专家张耀平在接受采访时指出，中国目前在各国中研究进度最快。<br />
<br />
&nbsp; &nbsp;<strong> 理论时速 可达两万公里</strong><br />
<br />
&nbsp; &nbsp; 记者获悉，首先将真空管道磁悬浮概念引进中国的，是毕业于西南交通大学的张耀平，在2007年成功申请国家自然科学基金项目“真空管道高速磁浮交通基础研究 ”后，他开始全力推进这一“运输革命”进入现实。<br />
<br />
&nbsp; &nbsp; 张耀平介绍说，最早提出真空管道磁悬浮运输概念的，是美国兰德咨询公司和麻省理工学院的专家。<br />
<br />
&nbsp; &nbsp; 2001年，经过努力，张耀平的研究获得了我国学界和政府的支持，他认为，目前这项工程所需的技术已经成熟。“专家们提出的每小时600―1000公里时速，是一个保守的对外口径，实际上所有研究者一开始就把这一运输方式的常规运行速度定位为每小时4000公里，而每小时6500公里是一个中期目标。近日在我与一名学者及其研究生座谈时，他们提出，真空管道磁悬浮列车的理论极限速度接近第一宇宙速度：可以实现每小时2万公里的速度。”<br />
<br />
&nbsp; &nbsp; 张耀平认为，目前科研项目唯一的需要，就是要从国家层面统筹建造第一条实验线路。<br />
