&nbsp; &nbsp; 南都讯 记者海鹏飞　今日零点三十分，南都记者电话联系了知名网络作家慕容雪村。“因小说创作而被抓起来，真是滑天下之大稽的事情”。慕容雪村说，自己写完网络小说《成都今夜请将我遗忘》后，成都有关方面曾专门开会批判该小说；《深圳向左天堂向右》发于网络后，也有官员不点名批评。<br />
