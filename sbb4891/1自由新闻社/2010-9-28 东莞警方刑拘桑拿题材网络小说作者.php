<br />
&nbsp; &nbsp; 南都讯 记者张书舟　天涯ID：天涯蓝药师，注册于2005年10月3日。他的小说《80后―――睡在东莞》最早于2009年6月17日发表在天涯杂谈，2009年8月9日，他因“杂谈敏感词多，让药师无法忍受”转而把小说改名《在东莞》连载于天涯的文学板块“舞文弄墨”，迄今为止该小说的点击已经超过了两百万。<br />
<br />
