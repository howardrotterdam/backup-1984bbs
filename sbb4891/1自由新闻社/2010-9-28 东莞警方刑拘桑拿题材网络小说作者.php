<br />
&nbsp; &nbsp; 律师说法<br />
<br />
&nbsp; &nbsp; 周泽：事关创作自由需慎之又慎<br />
<br />
&nbsp; &nbsp; 昨日，针对东莞警方以涉嫌传播淫秽物品罪刑拘网络小说《在东莞》作者元平一事，北京问天律师事务所律师周泽接受了南都记者的采访。此前“渭南书案”中，周泽也是纪实文学《大迁徙》作者谢朝平的律师。<br />
<br />
&nbsp; &nbsp; 有艺术价值的文学作品不属淫秽物品<br />
<br />
&nbsp; &nbsp; 《在东莞》一文，一直以网络小说的形式在天涯等网站连载，是否属于淫秽物品？<br />
<br />
&nbsp; &nbsp; 对此，周泽律师认为，刑法第三百六十七条明确规定，“包含有色情内容的有艺术价值的文学、艺术作品不视为淫秽物品。”作为网络小说，《在东莞》应视为有艺术价值的文学作品，不属于淫秽物品。<br />
<br />
&nbsp; &nbsp; 特别需要指出的是，是否有艺术价值，可能是一个见仁见智的问题。因此，判断一件有色情内容的作品是否属于淫秽物品，需慎之又慎，毕竟事关创作自由、言论出版自由。<br />
<br />
&nbsp; &nbsp; 最好由文学界去作出判断<br />
<br />
&nbsp; &nbsp; “是否淫秽物品，应该交给文学艺术专家去判断，而不应该由警察来判断。警方简单将一个作品认定为淫秽物品可能损害创作自由。”周泽认为，如果其是小说，属具有艺术价值的文学作品，就不属淫秽物品；即使小说内有一定写实成分，但作为小说，纪实作品仍然属于文学作品范畴。<br />
<br />
&nbsp; &nbsp; 同时，周泽表示，传播淫秽物品罪本身属于故意犯罪，需要取决于传播者对作品本身的认知，如果传播者根本不认为其传播的是淫秽物品，就很难说其有故意，从而缺乏主观故意。<br />
<br />
&nbsp; &nbsp; 此外，小说虚构情节，是合理的。涉及具体地名和场所，如果不实，最多是侵害名誉权的问题，不能以传播淫秽物品罪相责。<br />
