&nbsp; &nbsp; 南都讯记者韩成良　东莞市公安局工作人员介绍，此前市公安局网监部门已经关注到网络小说《在东莞》。市公安局宣传科昨晚回应，对于9月26日厚街警方到顺德抓该书作者一事暂时还不知情。据介绍，东莞镇街公安分局的一些行动，市局不一定会全部掌握。东莞所有镇街分局、甚至基层派出所外出行动抓人，回执文书上落款都是“东莞市公安局”。<br />
