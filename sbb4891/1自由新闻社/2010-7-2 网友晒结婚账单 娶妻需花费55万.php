													<h2>网友晒结婚账单 娶妻需花费55万</h2>
						
						
																											<div id="postmessage_717975" class="t_msgfont">网友晒结婚账单 娶个中上等老婆花55.4万<br />
2010-07-02《新文化报》<br />
&nbsp; &nbsp;某网友1日在网上晒出“长春娶妻成本账单”：55.4万元！该网友以男方家庭30万元家产、本人年收入6万元计算，最后得出结论：男方倾家荡产+男人不吃不喝工作4.2年=讨一个长春中上等条件的老婆的成本！<br />
<br />
　　 &quot;青春的堕落”随后自己跟帖：“悲剧了，做个男人难，做个好男人更难，做个有房有车的男人更是难上加难！”<br />
<br />
　　1.房子37.6万　一套(80平方米以上)，以均价4700元计，4700×80=376000元。<br />
<br />
　　2.装修5万。<br />
<br />
　　3.家电及家具3万。<br />
<br />
　　4.轿车5万　以普通小轿车为标准，5万元。<br />
<br />
　　5.办喜酒2万　以一般酒店200人(20桌)计算，包括自带酒、烟、糖。<br />
<br />
　　6.度蜜月1万　以国内为准，平均每人费用以5000元，总价1万元；<br />
<br />
　　7.恋爱费用1.8万　从恋爱到决定结婚这段时间，包括出去吃饭、买礼物、娱乐、旅游、送女友父母节日礼品等，平均每月500元的标准(女方出的不算)，谈3年，计500×12×3=1.8万元。<br />
<br />
　　各项成本合计55.4万元，以男方家庭30万元的家产，男人年收入6万元计，(55.4-30)÷6=4.2年。最后得出结论为：男方倾家荡产+男人不吃不喝工作4.2年=讨一个长春中上等条件的老婆的成本！<br />
<br />
　　热议：有人“害怕”　有人认为很现实<br />
<br />
　　该帖一上，立刻引起网友热议。<br />
<br />
　　对于这个帖子，网友观点各异。有人表示“害怕了，不结婚了”；有人表示“女人不物质，男人就不懂得珍惜。男人只有花钱了，才会心疼。爱你就会心甘情愿地为你花钱”；有人认为“现在要找女的，就得找不物质的，爱你的，能和你一起奋斗的，不然真结不起婚”；有人认为“这个结婚的女孩还不算物质，要求并不过分”；还有人对账单真实性产生怀疑，表示“算得不准”。<br />
<br />
　　对话发帖人<br />
<br />
　　超过个人能力就不理智了<br />
<br />
　　网友“青春的堕落”生于1986年。他说，该账单是朋友的结婚账单，那位朋友也是“80后”，在一汽工作，今年6月结婚时，由“青春的堕落”出任伴郎。<br />
<br />
　　为何晒出朋友的结婚账单？“青春的堕落”说，“我很理智很负责地对要结婚的人说上一句，社会风气已经形成，但结婚费用还是要控制在合理的范围内。这个社会是现实的，也是残酷的，请不要给自己的家庭带来不必要的负担。”<br />
<br />
　　“青春的堕落”表示，自己是一名房地产经纪，开着一家小型房地产中介。父母家里，条件也不错。对朋友娶老婆花费55.4万元，他认为朋友的花费基本上算是中等，而且结婚毕竟一生只有一次，还算是合理的理智消费，“但要针对个人能力，超过个人能力就非常不理智了”。<br />
<br />
　　“中上等老婆”怎么定义？<br />
<br />
　　“花费55.4万元讨一个长春中上等条件的老婆”，这是“青春的堕落”在帖子中得出的结论。那么，怎么来定义“中上等条件的老婆”？<br />
<br />
　　对此，“青春的堕落”表示，中上等条件包含很多，比如家庭背景、负担等，只是相对比较来说的条件好一些。同时，他认为，家庭背景包含的更多了，例如：学历、父母经历、家里环境、社会背景等很多元素组成。<br />
<br />
　　“结婚不会考虑花费”<br />
<br />
　　“青春的堕落”说自己目前没有女友，但已经购买了一套约173平方米的跃层住房。<br />
<br />
　　他将来娶老婆要花费多少钱呢？“青春的堕落”说只要碰到合适的女友，结婚不会考虑花费，同时认为“娶老婆我个人认为没必要让女方花钱”。<br />
<br />
　　为什么他的这个回答，与他前述的“结婚费用要控制在合理的范围内”，大相径庭呢？这位24岁的年轻人认为，自己收入高，结婚不用靠父母，就算不考虑花费，也不会给家里带去负担。<br />
<br />
　　随机调查<br />
<br />
　　20人中16人结婚要靠父母<br />
<br />
　　在“青春的堕落”看来，结婚支出是否需要靠父母，应是婚费投入多少的决定前提。<br />
<br />
　　在随机采访的20名刚结婚和准备结婚的市民，其中16人结婚都需要父母资助、贷款买房、举债结婚；另有4位不需要。其中14位，认为结婚费用应该双方共同承担；另6位认为应该男方拿80%、女方拿20%；认为全部应由男方负责的人没有。<br />
<br />
　　不过，如果女方家条件比较困难，男方家里条件优越，那么赞成由男方全部出资的超过一大半。反之，如果男方家条件比较困难，女方家里条件优越，赞成由女方全部出资的人数，只有1个。<br />
<br />
　　很多人贷款买房举债结婚<br />
<br />
　　这个账单，在长春具有普遍性吗？<br />
<br />
　　30岁的刘先生家在外地，他已在长春上班6年多，月薪3000元左右，今年5月完婚。他也计算了自己结婚的费用，50平方米住房加简单装修，没买轿车，这共计花费32万元。其中父母出资10万元，他借债8万元。“我们结婚还只是简单办了一下，现在结婚就攒钱还债，都不敢要孩子。”刘先生说。<br />
<br />
　　即将结婚的长先生，谈到结婚费用就挠头。“现在都讲究男房女车，房价这么高，结婚没个几十万元根本下不来，人家女孩听说结婚要贷款买房，都黄好几个了，现在是咬牙借钱买房，都不敢告诉女朋友。”“70后”长先生说。<br />
<br />
　　去年结婚的长春市民李女士，现在每月都要还房贷，她支持结婚多花点钱：“我和男友都觉得，一辈子就结婚一次，借钱也要办好点，不然以后多遗憾啊！”现实中像这样贷款买房举债结婚的不在少数。<br />
<br />
　　专家观点<br />
<br />
　　有多少钱办多大事儿<br />
<br />
　　对此，吉林省仁爱心理学校婚姻家庭指导师孙启山表示，现在结婚的基本上以80后居多，而他们是计划生育的第一代，受到两代父母的宠爱。祖父辈和父母都曾经有过苦日子的经历，都不想把自己吃过的苦在孩子身上重现，两代人把这种关爱放在了孩子身上，这就形成了80后的养尊处优，在某种程度上不能体会别人的感受，考虑得更多的是自己的幸福。<br />
<br />
　　而实际上，现在家庭中孩子结婚的费用是最主要支出，即使富裕也不应当炫耀和攀比，结婚更应该考虑自己和家庭的承受能力。现在的80后正在逐渐成为社会的主力、成为家长，如果一味追求攀比，是超前消费，举债结婚、贷款买房等，都会给自己生活带来一些压力。<br />
<br />
　　孙启山建议，结婚要审慎一些，从实际出发，有多少钱办多大事儿，不要给父母和家庭增加负担，包括金钱负担和精神上的负担，要学会换位思考。夫妻之间的感情，应该建立在情的基础上，建立在物质基础上，双方通过真诚劳动换来的幸福，比双方父母拿出的财产更珍贵，更能养成良好的人格，促进身心健康和家庭幸福。</div>

							
							
							
							
