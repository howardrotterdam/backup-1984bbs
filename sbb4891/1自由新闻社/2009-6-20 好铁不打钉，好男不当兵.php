核心提示：2009年，中国军队将征召12万余名高校毕业生入伍。而在2008年，这个数字还只是3.9万余名。而与2001年的2000余名相比，这个数字更是扩大了近60倍。在今年五六月份已经全面展开的预征工作中，解放军也将首次大规模从普通高校应届毕业生中直招士官。<br />
