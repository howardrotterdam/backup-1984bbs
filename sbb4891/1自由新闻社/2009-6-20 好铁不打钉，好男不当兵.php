――有关专家称，2009年的征兵工作无疑将载入国防和军队建设史册，这也是我国高等教育史上的一件大事：除部分急需专业少量应届大学毕业生作为生长干部特招入伍以外，我军首次大规模从普通高校应届毕业生中直招士官，高校毕业生入伍服义务兵役预征工作也首次在学生毕业前夕的五六月份全面展开。<br />
