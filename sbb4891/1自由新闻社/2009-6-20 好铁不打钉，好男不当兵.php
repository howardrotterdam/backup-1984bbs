高校征兵8年磨一剑<br />
<br />
今年是大规模征集高校毕业生入伍服义务兵役工作的开局之年。人力资源和社会保障部提供的数据显示：2009年高校应届毕业生人数约有610万人。综合各方消息，初步估算今年全国将征集其中12万余名入伍。而在2008年，这个数字还只是3.9万余名。<br />
