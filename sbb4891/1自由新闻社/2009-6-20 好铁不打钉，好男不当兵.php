在北京市2007年的征兵令中，重点强调了要将高校大学生、高素质青年作为征兵重点，整体提高军队的文化素质水平。图为刚刚应征入伍的青年。<br />
 2005年，北京大学在校本科生高明应征入伍。2年服役期间，他表现十分优秀。<br />
<br />
6月19日，济南警备区在山东师范大学、济南大学开展2009年应届大学毕业生预征登记工作。当日上午，共有500余人报名登记，图为报名大学生正在接受初步体检。<br />
<br />
解放军报6月29日报道<br />
<br />
●2009年高校应届毕业生人数约610万人，预计将征集12万余入伍<br />
<br />
●与2001年相比，2009年征集大学生入伍年度总量扩大近60倍<br />
<br />
●为鼓励高校毕业生服义务兵役，国家以空前力度出台8大优惠政策<br />
<br />
●从各地预征情况看，征集大学生入伍服义务兵役工作尚有努力空间<br />
<br />
进入2009年6月份以来，全国各地高校再次涌动参军热潮――<br />
<br />
6月16日，中华人民共和国中央人民政府门户网站发布信息：2009年高校应届毕业生应征入伍政策网上咨询将启动。6月18日至19日，网上咨询活动如期举行。<br />
<br />
6月23日，教育部高校学生司、国防部征兵办公室在教育部网站联合推出“国家鼓励普通高等学校应届毕业生应征入伍服义务兵役政策五十问”。<br />
