当年冬季，我国在部分高校开始进行征集新兵的试点。北京市选择北京工业大学等17所院校进行试点，上海市将徐汇、长宁、虹口、杨浦4个区部分高校列为试点。在湖北省，报名参军的高校学生竟达万余人。这一年，各地在校大学生有2000多人获准应征入伍。<br />
<br />
随后，试点范围逐年扩大。截至2004年，西北五省（区）兵役机关从29所高等院校征集在校大学生539人。国防部征兵办公室称，这一做法对全国有借鉴意义。<br />
<br />
2005年3月，全国“两会”期间，十届全国人大军队代表汤玉英建议：从全日制高等学校在校学生中征集新兵，应写进《兵役法》。她的理由是，在校大学生入伍后发挥在法律、机械、管理、计算机、信息工程等方面的专业优势，有效地促进了部队全面建设，但全国各地适用征集大学生士兵的政策和规定标准不一，缺乏统一的规范指导，不利于征兵工作的开展。<br />
<br />
2005年10月底，国务院、中央军委冬季征兵命令宣布：全面开展征集在校大学生工作。这一年作为一个重要转折点，成为一个分水岭：在校大学生应征入伍工作，由此全面启动。向教育要人才、到院校选兵员，越来越得到广泛认可。<br />
<br />
2008年2月13日，国防部征兵办公室发出通知，要求认真做好各级各类院校的征兵普查工作。4.8万余名普查员走进全国2.2万所中等以上学校，对328.3万名即将毕业的男性学生抽样调查。232.3万名潜在征集对象纳入兵役机关视野：即便充分考虑升学和就业，并假定全部毕业生首选就业，2008年冬季仍有144万名各类院校应届毕业生可以应征，加上88.3万名高考落榜生，合计有 232.3万名高中以上应届毕业生成为潜在征集对象。<br />
<br />
与2001年的2000余名相比，2009年征集12万名大学生入伍，年度总量扩大近60倍。总参动员部征集动员局局长侯岐山说，许多以前看起来简直是奢望、做起来大有难度的事，如今已经变成了现实。<br />
<br />
2009年3月5日，十一届全国人大二次会议上，中共中央政治局常委、国务院总理温家宝在政府工作报告中论及民生和就业问题时提出，高校毕业生“到农村基层服务和参军入伍，给予学费补偿和代偿助学贷款”。<br />
<br />
同年6月2日，《中国青年报》题为《参军入伍成大学生就业新途》的专稿认为，2009年的就业寒流中，大学生参军的另外一个含义是：能够一定程度缓解就业压力。早在2008年冬季征兵期间，《中国新闻周刊》就刊发特稿《征兵政策大幅调整：大学生参军缓冲就业压力》提出，大学毕业生数量越来越大，促使大学生通过参军缓冲就业压力，也为征兵新政的推出提供了现实基础。<br />
<br />
江汉大学学工部部长、武装部部长范纯新对此有着自己的看法，他把想参军的大学生分三类：第一类崇拜军人，向往军营生活。他们大部分曾与军营擦肩而过，作为大学生征集入伍，对身体要求相对较低，一般说来能顺利通过；第二类想到部队求发展。目标是争取考军校或者能提干当军官；第三类家庭比较困难。同时有几个兄弟、姐妹念书的大学生，想借此缓解家庭压力。东北师范大学历史系团委书记董俊认为，大学生投身军营，除了理想的成分以外，还有现实方面的考量，体现了成熟理智的一面。<br />
<br />
大学生本人又是怎么想的呢？记者通过互联网问卷调查、电话提问、现场座谈等不同形式，追踪采访了正在进行预征登记、已经入伍和已经退役的三类大学生人群。<br />
<br />
今年推出的相关政策规定，大学生入伍服义务兵役没有专业限制。郑州澍青医学高等专科学校口腔专业的范子男，想从军同时又很喜欢口腔专业。在预征登记中，他最关心的问题是：“入伍后我能不能还干口腔啊？”现场提供咨询的人员告诉他：部队会考虑尽量让你的专业对口，但军人以服从命令为天职。“我喜欢看军事剧，《突出重围》、《DA师》、《士兵突击》、《女子特警队》，每部连续剧都看好几遍。 ”一位网名叫“小白”的2009年应届毕业女大学生，特别盼望能有机会穿上军装。听说女生不在预征之列，只能在冬季征兵时报名应征，她显得非常失望：“那至少还得等几个月呐！”<br />
<br />
2007年，清华大学新闻传播学院女生贾娜从军入伍，被誉为“清华第一女兵”。同学问她为什么要当兵，她回答：“我去减肥啊！”其实，贾娜本人并不显胖。1985年出生的华南理工大学毕业生黎结华，2008年入伍后分配到海军某部成为一名列兵。如愿穿上军装，黎结华非常满足：“我圆了自己的梦！”在黎结华看来，军营是块成才的沃土，他愿意在这片沃土“更好地锻炼自己”。一些正在服役的大学生提醒即将毕业的学弟学妹：如果紧盯就业而参军，会很难适应部队生活。<br />
<br />
2007年11月，北京大学光华管理学院大学生高明退役返校。他自我评价：“该来，则来；该去，则去。”《中国青年报》军事部主任李雪红，对高明的入伍动机有过一番追问――<br />
<br />
为了入党？高明在大二就入党了。<br />
<br />
为了考学？高明本身就是北大的。<br />
<br />
为了在部队提干？可高明想的是尽完义务回去继续念书。<br />
<br />
为了就业？谁都知道，北大光华管理学院的毕业生用人单位抢着要。<br />
<br />
为了减免学费？高明本身就是国家“西部助学计划”的受益者，学费、生活费国家都包。<br />
<br />
什么都不为？有人说：“那他有病啊？！”<br />
<br />
高明在《我为何南行从戎》的文章中写道：“北大自建校之始，兴亡感与责任意识便成为一种自觉而内在的承担，这种兴亡感与责任意识促使每一位北大学子奋进。从军报国是我生命中最大的荣耀！”<br />
<br />
就业、锻炼、圆梦、报国、荣耀……当记者把这些字眼放在一起重新审视的时候，对大学生选择入伍有了一种更深层的理解和认同：所有这一切叠加在一起，有激情也有理性，有功利也有理想，惟其如此，才是一种真实的客观存在，才更清晰地彰显出我们这个时代的进步与宽容。 <br />
<br />
退役大学生士兵话得失<br />
<br />
22岁的李高杰看上去很壮实，他说这是在部队摸爬滚打训练的结果。提起自己的战友，他停顿了几秒，有一种想哭的感觉。<br />
<br />
李高杰是清华大学土木系学生，2005年入伍，2007年11月退役返回清华大学，重新读大一。在刚返校的半个月里，李高杰几乎天天晚上做梦，梦见自己又回到部队。“我还想回到部队上去，特别特别想。”李高杰对记者说，“部队是一个比较简单的地方，每天就是训练、学习、劳动、吃饭、睡觉，没有别的；士兵之间的关系也很好，人际关系很简单。而在清华，压力特别大。”<br />
<br />
很显然，李高杰“收获”的是战友之间的纯真与友谊。采访中，来自不同大学的退役大学生们有一个共同的体会：在部队最大收获是培养了良好的集体观念、团结友爱的作风、吃苦耐劳的精神和强健的体魄。<br />
<br />
当然还有许多具体的收获。2007年底，北京建筑工程学院机械工程与自动化专业的张维思从军营回到校园。两年前，刚上大一的张维思报名参军，在部队他连续两年被评为“优秀士兵”，回到校园后按照学校当初的承诺顺利“专升本”，改读本科一年级。与张维思同年入伍的8个同学，有7个返校继续学业，另外一名女生则直接考进了军校。一个更加令人欣喜的变化是，经过8年探索实践，各地对大学生应征入伍的种种优惠“举措”，在2009年终于上升到国家政策层面。<br />
<br />
两年前，张维思在接受《中国新闻周刊》采访时说，高考只考上专科，让他对学习完全失去兴趣，是两年军营生活焕发了他对学习的信心。在张维思看来，这种转变对他个人影响深远：“部队是一个很特殊的地方，在那里最大的感受就是没有标签，没有出身，只要适应能力强，能力提高快，就能赢得别人的尊重。”<br />
<br />
到部队当兵有得有失。对清华大学化工系的肖丁宜而言，最大的“损失”就是：上课已经听不懂老师在说什么――当兵两年回来，专业课都快忘光了。对此，许多退役大学生也有大致相同的解释：当兵就短短两年时间，你必须全身心投入到部队中去；在部队上你还抱着专业书籍不放，那就有点儿把自己当外人的感觉，很难完全融入集体中去。<br />
<br />
关于“补课”，北京大学学工部部长马化祥明确表态，院系可以做些工作，但不会专门“开小灶”。退役大学生们对此反应平静：当初入伍时从一个普通学生成了一个普通士兵，现在又从一个普通士兵成为一个普通大学生，学业上谁也不会伸手要“额外的优待”。<br />
<br />
2009年6月，第二炮兵政治部文工团参加第九届全军文艺会演的主题歌舞《骄子从军》上演，包括北京大学、中央财经大学、北京科技大学等10多所高校大学生在内的首都各界人士3000多人次观看，并给予好评。《骄子从军》以新中国成立以来北京大学第一位在校入伍大学生高明的事迹为素材，反映当代大学生士兵携笔从戎、精武报国的心路历程。<br />
<br />
显然，高明已经成为大学生入伍进而在军营茁壮成长的一个样本：男儿有志挥金戈，他从“普通战士”到“导弹精兵”，一年就掌握了许多专业技能；从“三好学生”到“合格战士”，以顽强的意志实现人生的跨越；从“优秀士兵”到“优秀党员 ”，把党的创新理论作为成长进步的最大动力……“大学毕业后去当兵，不光是为了保家卫国、奉献青春，还为了锻造自我、历练青春。应征入伍对于我们来说，是机遇，也是挑战，更是一个全新的人生平台。”宁波大学许多退役大学生士兵在说这番话时，目光中多了一些坚定与成熟。 <br />
<br />
各方评说展示新坐标<br />
<br />
为什么大规模征集高校应届毕业生入伍服义务兵役？<br />
<br />
总参动员部部长白自兴接受采访时，一连讲了4个“迫切需要”：一是军队现代化建设的迫切需要。有的士兵操作着价值几百万甚至上千万元的新装备，没有较高的文化素质和专业技能行吗？二是完成多样化军事任务的迫切需要。兵员文化素质高、反应快，学习和掌握新装备能力就越强。三是部队人才队伍建设的迫切需要。如果士兵群体与军官队伍之间的学历差距越来越大，就不符合科学发展观要求。四是社会对退役士兵文化素质和技能的迫切需要。简言之：“来者越优秀，回去后越好安置。”<br />
<br />
兵员素质与兵员结构，两者之间互为前提又互为因果：优化兵员结构，呼唤良好的兵员素质；兵员素质的整体提高，又促进兵员结构的进一步改善。中国特色军民融合式发展成果，无疑提供了这一“前提”，同时又凸显了这一“因果”。《2008 年中国的国防》白皮书指出：“将国防建设有机融入经济社会发展之中，形成经济建设和国防建设协调发展的科学机制，为实现国防和军队现代化提供丰厚的资源和持续发展的动力。”大学生作为重要人才资源被军队重视和吸纳，是中国特色军民融合式发展的题中应有之义。<br />
<br />
事实上，大学生入伍服义务兵役已经给部队建设带来全新变化。从沈阳化工学院入伍的大学生士兵杨曦晔发挥自身专业优势，积极进行“烟幕的战术使用”等课题攻关，取得重要突破。从沈阳理工大学入伍的王士玉被分配到二炮某基地，利用所学计算机专业知识，制作了多个有价值的多媒体训练教学课件。曾任某集团军参谋长的王聪民介绍说，仅从2001年到2004年3年间，他所在集团军征集在校大学生和技能型青年2896名，其中890人被优选到总部、兰州军区和集团军训练机构培训，138人考入院校，276人被选取为士官，186人担任了教练班长，在部队建设中发挥了不可替代的作用。<br />
<br />
部队尊重人才，更褒奖作出贡献的人才。南京军区某摩步旅退役士兵、二等功荣立者、复旦大学大四学生唐三勇介绍，近4年来复旦大学先后有54名大学生入伍，2006年入伍的7名大学生除1人荣立二等功、1人荣立三等功外，其余5人都被评为优秀士兵，全部担任骨干。宁波市首批入伍的31名大学生，1人荣立二等功，4人荣立三等功，9人入党，15人当上正副班长，16人受部队嘉奖，22 人被评为优秀士兵。宁波大学学生徐辉荣立二等功和三等功各1次，还被武警部队评为“学雷锋标兵”，被驻地无锡市评为“十大杰出青年”，并光荣当选为共青团十五大代表。<br />
<br />
这些学生退役返校后的表现更是可圈可点。以宁波大学为例，返校复学生中，获国家级专利1项，省市级奖励2项，校级各类奖励31项，奖学金获得者12人；已毕业的首批21名退役学生中，有7人考上了公务员，其中1人考上海关，6人考上公安，其余都找到满意的工作，就业率达到100％。宁波大学学工部部长严建雯说，学校原来担心退役返校生跟不上学习，让人意外的是，他们个个表现优秀，令人刮目相看：在校园里走路最端正的是他们，宿舍生活最整洁的是他们，师生面前最有礼貌的是他们，对院系、集体工作和事业最关心、最积极的还是他们……<br />
<br />
面对这一系列可喜成果，国防大学政工教研室副主任公方彬教授欣喜之余表达了自己的期盼：“我们的有关制度还有待完善。”他说，当前世界上大多数国家从法律上规定大学生要服兵役，逃避兵役不仅仅是一种耻辱，违背社会道德规范，还要受到法律制裁。今年有关政策就有一个明确规定，凸显了“依法征兵”与“依法参军服兵役”的统一：高校毕业生应征入伍变更就业协议，不属于违约。<br />
<br />
胡锦涛总书记在党的十七大报告中强调，要“加强公民意识教育，树立社会主义民主法治、自由平等、公平正义理念。”郑州大学公民教育研究中心教授王东认为，责任意识是衡量一个公民国家主人意识强弱的重要尺度，公民意识教育必须把强烈的权利意识教育与自觉的责任意识教育统一起来，必须把国家意识教育与民族意识教育统一起来。军队一些思想政治工作者提出，国防观念应该成为公民意识的重要组成部分，高校应届毕业生争相入伍是国防观念深化、公民意识增强的一个生动体现。<br />
<br />
记者采访中了解到，各地高校应届毕业生预征登记总体趋热，但冷热不均；各方给予热情支持，但有些工作还不够细致。以河北省为例，今年高校应届毕业生总量首次突破30万人，男性毕业生总数为12万多人，截至6月14日，全省高校毕业生入伍预征报名不足4000人。河北省教育厅副厅长闫春来认为，这与宣传不够有很大关系。这也说明，征集高校应届毕业生入伍服义务兵役工作尚有许多努力空间。诸如高校毕业生应征入伍服义务兵役“优先报名应征”、“优先体检、政审”、“优先审批定兵”、“优先选拔使用”等政策规定，还要广泛宣传，力争做到家喻户晓。<br />
<br />
国家大规模征集高校毕业生入伍服义务兵役，是一项利国利民利军的重要举措。从某种意义上讲，预征登记是征兵工作的重要一环，也是强化国防观念、增强公民意识的重要载体和手段。教育部思政司思政处处长徐艳国认为，大学生只有更多地了解国情，了解社会，了解实际，才能更进一步明确自己的人生方向和目标，因为在这些工作中他们也受到了锻炼。 （董强）<br />
<br />
大学生入伍8大优惠政策<br />
<br />
为鼓励高校应届毕业生入伍服义务兵役，教育部、财政部、总参谋部、公安部等部门以空前力度出台优惠政策――<br />
<br />
1、对应届高校毕业生实行预征制度，在五六月份进行；<br />
<br />
