													<h2>韩寒未必的末日：没事别株心~</h2>
						
						
																											<div id="postmessage_30610" class="t_msgfont">不太喜欢末日这个词，因为这个词过于绝对。那么对于这篇文章，自然也不会喜欢了。<br />
<br />
本人不想用极端的言论来批评文章的作者，但我确实不赞成作者的观点。韩寒这人我不算了解，但他的情况我想并不是特例。在他写些文章的同时，你我也在写类似的文章，成百上千的人也在写类似的文章：内容生动活泼、风趣幽默、义愤填膺、字字千斤，可谓是嬉笑怒骂，应有尽有。只不过由于韩寒的年龄、韩寒的经历、韩寒的个性、韩寒的文笔，一系列的条件把他被推到了台前，使他成为了热议的话题。为什么在我说这些内容的时候，有人认为我看问题深刻准确，不骂爹不干娘，黑色幽默用的恰到好处，到了韩同学这里就成为了“懦弱无用的，乃至无脑的青年人”使用的枪，真是奇怪。<br />
<br />
韩寒想必大家比我熟悉得多：一个靠写作起家的人，一个喜欢赛车的人，一个看不惯就要说，说的不爽就要写的人。看一看他的成长历程，你会发现他有着相当多的与体制不相符的光荣事迹。 似乎他本身就代表着争议。就这么争议了若干年，他依旧活得好好的，还是一贯的那幅老子谁也不吊的神气嘴脸，照样活得自由自在，光彩照人。 你说他的这幅嘴脸是做给你我看，还是做给怯懦的年轻人看呢？ 他不会在刚开始写作的时候就预示到将来的红火，然后先自定义一番吧？ 我写文章的时候，常常有了想法一屁合成，分了时间段就如同便秘一样挤不下来，在这个世界上，我认为也只有政治精英才能够把文章弄的如橡皮泥般可塑，想怎么变就怎么样变。你也说了韩同学不成熟，不懂政治，那他应该不会如英明领袖一样在写作前先想好迎合谁再挥动笔墨吧。所以你说的他为了万千年轻人的追捧而写作抨击不成立。 <br />
<br />
文章也提到了韩寒没有文化。我不知道该如何定义有文化，不过我知道，如果把讽刺定义为没有文化的表现，那么杨柏一定没有文化、李敖一定没有文化、鲁迅一定没有文化、就连毛泽东老板也一定没有文化（在我看来，毛是中国老板当中，最英明也最有文化的一位，当然他们之间最多是方式和造诣上的有所不同）！如果有没有文化这个句子能被某人用标准评判出来，他一定是非常了不得了。<br />
<br />
再多说一句，韩寒的文章其实真的不厉害，他还没有电视台，报纸来撑腰呢，如果文章的作者生活在白色恐怖的米国，想必早就连死的心都有了~<br />
<br />
至于死啊，断头啊之类的话语，按照共产党的话来说，革命者不怕牺牲，用我自己的话说，现在和谐社会了，刘晓波还在多功能小黑屋里活得好好的，你瞎操什么心呢。<br />
<br />
生活本身就是丰富多彩的，谁也不应该剥夺谁应有的权利。我说话，你不爱听可以离开，谁有不会逼你。没必要搞的大惊小怪，仿佛看透了某人真实的嘴脸，四处奔走大声疾呼，像我奉劝你一样奉劝他人闭嘴。我觉得人没有那么高尚，也不用那么低俗！<br />
所以最后请求作者，咱们各干各的事，没事别株心~</div>

							
							
							
							
