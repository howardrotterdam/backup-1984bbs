													<h2>韩寒的末日</h2>
						
						
																											<div id="postmessage_30581" class="t_msgfont">韩寒的末日<br />
<br />
文章提交者：猫眼看奸 <br />
<br />
很多angry youth们很喜欢韩寒，把他推崇为精神偶像。<br />
<br />
当然我也挺喜欢他的，特别是初中第一届新概念的时候。现在我还记得他的“穿着衣服洗澡”的调调，当时为之兴奋激动不已。<br />
<br />
我偶尔会买他的书，喜欢《像少年啦飞驰》。<br />
<br />
我也喜欢看他的博客，虽然他的很多观念我都不赞同。<br />
<br />
韩寒的行为，其实是被集体精神所逼迫的，是看不见的集体精神无形中的推动他在写作乃至叫嚣。<br />
<br />
借李大眼的话来说，韩寒是个没文化的人。<br />
<br />
这是事实。<br />
<br />
他的书思想性或者说艺术性是很空白的，但是他的文笔好，他跟安妮宝贝之类的区别仅仅在于风格不同。<br />
<br />
文笔的风格。<br />
<br />
他在极不成熟的时期开始勉强自己写作（也可能是他当时唯一的出路），使自己的思想“发育”过早停止在17岁。<br />
<br />
一路看着他走下来，我觉得挺悲哀的，他现在的才气还没有当年耀眼――虽然他现在可以说已经光芒四射了。<br />
<br />
<br />
<br />
扯远了，我的主题是韩寒的末日。<br />
<br />
<br />
<br />
最近韩寒的博客多篇博文被和谐。<br />
<br />
同时发生的是各大论坛上韩寒的人气持续高涨。<br />
<br />
而知名的或不知名的文化界人士也开始捧韩寒。<br />
<br />
表面上韩寒得到了大众跟部分文化人士的认同。<br />
<br />
而事实上，韩寒在被隐性的引导向一条道路。<br />
<br />
从他开始指责GOV开始，到他的指责行为被部分大众认同，使他自己产生了认同感，或者毋宁说，一个才气将尽的作家开始找到一根稻草，使自己保持人气的稻草。<br />
<br />
以至于到现在为止韩寒的博客全部是挑GOV的刺儿的博文。<br />
<br />
他习惯于写这样的文字，甚至也许可能根本没有这样的长期效果跟反应。<br />
<br />
<br />
<br />
当我知道他的最新博文被和谐的时候，我觉得，仅仅是我觉得，韩寒的末日不远了。<br />
<br />
而杀死他的，是盲目追捧他的大众，这些懦弱的大众把他当作曙光，其实仅仅是为了发泄不满情绪。<br />
<br />
而更丑陋的是刻意的追捧他的人们，刻意的引导他的人们。<br />
<br />
用脚想都知道这些人的背景跟目的。<br />
<br />
<br />
<br />
韩寒反对的，我也反对。<br />
<br />
我不认同的是他对于反对的体制所采取的行为。<br />
<br />
这种行为是可笑的，甚至可以说是愚昧的。<br />
<br />
当然这需要很大的勇气跟决心，但是事实上几乎可以说这样的行为是无效的。<br />
<br />
捧他的人，大多数是angry youth，而不管这些人的立场是左是右，其实都只是angry youth，都是懦弱无用的，乃至无脑的青年人，他们只是感性的反对这些丑恶，但是他们没有任何来自自身的对于现实的理性的考虑。<br />
<br />
他们有决心采取行动吗？振臂一呼？<br />
<br />
休想。<br />
<br />
当韩寒被送上断头台的时候他们会愤怒会跳脚大骂，但是当暴力来临，当主人要对他们强制断粮的时候，他们就沉默。<br />
<br />
这是中国大多数现代人的共性。<br />
<br />
<br />
<br />
所以，韩寒其实只是被人当枪使的人们。<br />
<br />
他是高尚的，是勇敢的，是有骨气的。<br />
<br />
但是是无用的。<br />
<br />
<br />
<br />
在这个体制内，任何主动直接的反抗都是无效的。<br />
<br />
不是没有先例的。<br />
<br />
更强烈的反抗的领导者，要么死去，要么漂泊在外。<br />
<br />
<br />
<br />
法国大革命的反复爆发及彻底性，有没有决定性的原因呢?<br />
<br />
我觉得有的。<br />
<br />
那时有伏尔泰有卢梭有孟德斯鸠。<br />
<br />
他们的贡献在于使人们了解到不公平的现实产生的原因，根源，改变的途径，未来社会的架构。<br />
<br />
最重要的是他们在人民中普及了关于人的存在价值的理论，使人民全新的认识了自我，人民认识到自己的强大。<br />
<br />
那时即使不出现拿破仑，也会出现索雷尔、雷诺或者拉莫尔什么的。<br />
<br />
领导人的名字不重要，重要的是跟随他的人民知道自己追求的利益。<br />
<br />
而不是某集团那些趁火打劫的强盗。<br />
<br />
志同道合的为捍卫共同利益战斗的人民，他们的进攻是无坚不摧的，他们斗争的决心是无比坚强的，他们不会在暂时的胜利之后就摇身一变成为分赃强盗、打手跟奴民。<br />
<br />
他们会持续战斗到底，直到天赋人权的普世价值成为文明世界的通行证。<br />
<br />
在和平时期，他们在议会中战斗，不惜挥拳。<br />
<br />
<br />
<br />
在韩寒在体制挥拳叫嚣的时候，他是孤独的。<br />
<br />
而聪明的卢梭孟德斯鸠，他们温和的进入人民，劝导人民，让每一个人都成为卢梭孟德斯鸠。<br />
<br />
当法国人都知道自己跟国王是平等的时候，他们还需要国王吗？？他们还需要三个阶级吗？？<br />
<br />
这就是区别。<br />
<br />
<br />
<br />
而两个世纪后的人们，还在有意无意的引导着勇敢的韩寒在火上跳舞。<br />
<br />
当有一天，人民分清楚了“权利”与“权力”的含义时，当他们明白了平等的意义是存在价值的平等，社会赋予权利的平等，而不是财富的平等和享受的平等时，我们的希望就来临了。<br />
<br />
但不是现在。<br />
<br />
现在，我只能看着，什么也做不了。<br />
