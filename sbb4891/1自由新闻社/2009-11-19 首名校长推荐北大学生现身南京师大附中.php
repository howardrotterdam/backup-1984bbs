													<h2>首名校长推荐北大学生现身南京师大附中</h2>
						
						
																											<div id="postmessage_333773" class="t_msgfont">江苏出现首名校长推荐北大学生 面试质疑北大<br />
<br />
2009年11月19日08:19<br />
<br />
<img src="http://img.hexun.com/2009-11-19/121741320.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　南师附中王占宝校长要举荐给北大的学生叫匡超！记者昨天在南师附中公告栏看到，该校高三（1）班的学生匡超已经确定为学校即将举荐给北大的人选，目前正在接受“公示”，成为江苏校长实名推荐上北大的“第一人”。在北大公布“举荐权”后仅两天的时间，南师附中就率先出炉了要推荐学生的名单，不得不让人大吃一惊。然而记者采访发现，对于这个决定，学校里的反应还相当正常。“匡超啊，超牛的！文科生里他理科学得最好，理科生里他文科学得最好！”该校已被清华提前录取的学生赵鹏竟毫不掩饰对匡超的佩服之情。<br />
<br />
　　两天出炉“推荐学生”<br />
<br />
　　“我们推荐给北大的学生必须具备两个标准：第一、学业发展优秀；第二、综合素质发展优秀。”南师附中周俊副校长告诉记者。对于学校为何这么快就定下学生名单，周校长表示，学校的工作都是按照规范的程序来的。首先由学生自己报名，学校任课老师再推荐；接下来根据学校多年来已经形成的优秀学生的《推荐条例》进行评价考核；最后由学校研究决定。目前南师附中的推荐学生已经出炉，为高三（1）班的匡超同学，现在学校正在进行公示。<br />
<br />
　　“这样跟你说吧，在我们看来，好学生有三种：一、平时学习成绩好；二、学科竞赛搞得很好；三、学生活动能力强。一般来说，能做到其中一点就很不容易了，不过高三（1）班的匡超是超牛的，他三样都很厉害。”正在北京参加物理集训的赵鹏是南师附中高三（4）班的学生，他在电话里告诉记者，“学校举荐匡超，羡慕的人很多，但敢说不服的我想没几个！”<br />
<br />
　　中选的是超级“牛人”<br />
<br />
　　“匡超确实很不错！”提到自己的得意门生，匡超的班主任刘文庆老师相当自豪。“首先，从学校三年来学习成绩跟踪下来的结果显示，他是年级第一；其次，他是班长，性格很阳光，搞活动能力强，大家都很拥护他。再次，他有竞赛特长。高一时就获得南京市英语电视口语大赛一等奖；08、09年都拿到省物理竞赛一等奖，今年获得了保送资格；08年省数学竞赛三等奖、09年又拿了二等奖！总之一句话，这个学生综合素质很强！”<br />
<br />
　　记者了解到，匡超是丹阳人，小学时在一所农村中心校就读。“他的学习习惯、生活习惯都很好，基本上不用我们操心。”对于儿子，匡洪浩很满意： “小学就是故事大王，还自编相声表演，又自学了架子鼓。到了中学，不仅学习成绩不用烦，奖也是一个接一个拿回家。我这个儿子，只要投入什么事，就一定会把它做好。”<br />
<br />
　　他已与北大“过了招”<br />
<br />
　　对于此次“中选”，匡超很自信，一点没有想象中的负担。“我在学校知名度还蛮高的吧，所以名单出来也没引起什么轰动，大家可能觉得我还是可以的吧！”匡超告诉记者，自己一直对北大情有独钟。“我也是才知道自己被校长举荐到北大，因为我有保送生资格，如果举荐成功，我可以免笔试直接参加北大1月份的面试，只要面试通过我就可以早日圆梦了。”<br />
<br />
　　不过说到北大的面试，匡超笑着和记者说起前不久北大老师来学校给保送生开座谈会的一个小插曲。“那天的座谈会上，我向北大一位校长提出了一个问题：‘以前的北大是一所大学撑起了一个民族的脊梁。北大经过多么年的发展，是不是仍然有这样一种教育定位？’当时这位校长回答我说：‘特定的时代大学的使命是不一样的，但是作为大学培养优秀学生的目标是不会改变的。’对于这个回答我能接受，不过在关于人才流失问题上，我对他的回答提出了质疑。现在北大、清华两校出国的学生很多，但很多人都留在国外工作了，对于这种现象校长回答说并不存在人才流失，要用国际的眼光来看，人才出国也能带来一种促进。但是我很快提出了自己的想法，如果钱学森当年不回国，中国的核技术突破是不是要推迟好几年？人才出国可以，但如何提高吸引力，把这些人才再召回来很重要。”<br />
<br />
　　记者了解到，匡超与北大这次意外的“过招”已经让他给北大老师留下了深刻的印象。<br />
<br />
