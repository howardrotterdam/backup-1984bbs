　　郴州皇家钱柜的保安为什么会如此之牛？据当地群众反映，是由于其强硬的后台和复查的关系网络，甚至有公安局的领导参股其中。该场所开业之时，本身就无证无照，手续不全，但是在某些官员的保护下，还是照常营业。就在其刚开始非法营业之时，郴州某报准备对其进行报道，然而不知道为什么，报纸大样已经出来，后来胎死腹中。<br />
　　<br />
　　“3.24”皇家钱柜打人事件发生后，郴州警方已经介入调查，郴州本地一家媒体也进行过报道，说是一名顾客为了换包厢与保安大打出手，10多名保安拿着铁棒进行了还击。而录像资料显示：那些保安在打人的时候，崔先生等人连呼救的力气都没有，哪还能够对保安大打出手。<br />
　　更加匪夷所思的是，记者到病房里采访被打的崔先生及其女友时，他们表现出来的不是气愤，而是害怕。那个当时跪在地上为男友求饶的女孩子，在记者面前什么都不敢说。<br />
　　<br />
　　同时，据崔先生介绍，直到现在，还不知道公安机关将打他的人拘留没有。郴州皇家钱柜保安之所以敢如此放肆，是因为其背后强硬的靠山让其有恃无恐。当地群众说，郴州的社会风气就是在这股歪风之下形成恶性循环，他们期待着当地政府及职能部门对此进行整治，还他们一个和谐郴州。<br />
