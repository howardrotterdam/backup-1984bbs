													<h2>中国立法剥夺了动物的“五大自由权”</h2>
						
						
																											<div id="postmessage_452939" class="t_msgfont">专家披露五大自由权为何未写入《动物保护法》<br />
来源：青年周末<br />
<br />
◎文/《青年周末》记者 李佳<br />
　　清晨8点刚过,故宫东北角胡同里的一个单位小院，除了树上偶尔嘎嘎鸣叫的喜鹊，院子里寂静无声。这是9月22日的中国社会科学院法学研究所，还未到上班时间，大部分办公室的大门紧锁，只有楼道里的保洁员忙着擦地板。而在一楼尽头的社会法研究办公室里，常纪文教授已坐在桌子的旁边等候记者的采访，比之前双方约定的时间提前了15分钟。<br />
　　他的学生蒋先生告诉记者，常教授通常清晨7点多出现在办公室开始一天的工作。“他平时做人很低调，工作也非常勤奋，像大部分的学者一样，你可以用温和、儒雅、谦逊等等之类的词语来形容他。”<br />
　　但是自从9月18日《动物保护法》（专家建议稿）正式对外公布，作为法案的首席起草人，这个低调温和的学者瞬间被抛向舆论的风口浪尖，面对种种非议甚至污言秽语，保持沉默显得很难。<br />
　　我昨天晚上又接到了威胁电话，一上来就骂，说我是神经病，我现在真的很愤怒！<br />
　　常纪文教授说着说着就站起来。此时距离《动物保护法》（专家建议稿）正式对外公布仅仅五天。<br />
　　在《动物保护法》（专家建议稿）公布的第二天，助手蒋先生就接到几十通相关电话，其中不乏张口就骂者。“我收到的反馈只是一部分，常老师接到的肯定更多，他的信箱在网上是公开的，恐怕有几百封。绝大部分的反馈都是积极或者赞扬的，比如网上调查显示80%的人支持《动物保护法》出台。但也有一些无聊的信息，这些大概占十分之一二。”<br />
　　“大家提批评建议，认为我们应该如何改进这个法，这些都是善意的，我都接受而且非常非常感谢。但如果这部法你连看都没有看，一上来就骂，还威胁要在网上联合攻击我，我只能对我的助手说，我们没必要跟他计较。”整个采访过程中，常纪文教授屡次提到类似的事情，愤愤不平的语调里透着一丝无奈。<br />
　　不管我们现在是否贫穷，不管人权得没得到保护，我们都要人道地对待动物，这也是人权保护的一种延伸。<br />
　　面对人们对《动物保护法》（专家建议稿）“超前”、“过于奢侈”的解释，常纪文教授一遍一遍的解释。<br />
　　9月18日，中国首部《动物保护法》（专家建议稿）始一发布，就引起社会各界的广泛关注。按照这部专家建议法，乱杀狗、遗弃猫情节恶劣将追究人的刑事责任；给动物强制喂食等行为将违法；屠宰动物要以给动物最小痛苦的方式进行；斗鸡斗狗等宠物搏斗节目也将以法律的形式予以禁止。<br />
　　不少人认为，在中国当下的社会经济环境中，人的权利尚未得到完善的保护，现在谈保护动物的权利是否有些为时过早？而为了动物让人承担刑责似乎是否有些小题大做？还有的网友认为，“《动物保护法》的条款太过超前，这是一部奢侈的法律”。<br />
　　以上的回复还算礼貌，更有甚者当即拿起手机给常纪文的助手发来短信：“你很无聊”，“你是不是吃饱了撑的没事干”？“鄙视你一万年”！<br />
　　面对理智的质疑，常纪文耐心解释：“很多人还是没仔细看我们这部法。这个法在前面的立法目的里写得很清楚，我们立法的底线是反对虐待和遗弃动物，而不是大谈给动物多少福利，所以叫《动物保护法》而不是《动物福利法》。不虐待动物就是不给动物以不必要的伤害和痛苦。我们并没有奢谈现在要给动物多少权利或福利，只是鼓励人们如果有条件就多给动物福利，没有条件就少给动物福利。怜悯生命，不虐待动物，这也符合中华民族传统的道德观念。”<br />
　　面对污言秽语，常纪文反击：“我是一个学者，我没有任何利益在里面，我家也从没养过宠物。我小时候被狗咬过，现在腿上还有印儿。”<br />
　　在学者们看来，《动物保护法》的立法目的绝不仅仅是保护动物不受虐待。规范经济动物的饲养环境，促进我国动物产品的出口；通过处罚遗弃、虐待动物的行为维护社会秩序和社会稳定，这些已是迫在眉睫的现实问题。“我举两个例子，一是国内饲养的动物不符合国际上动物福利贸易标准的要求，导致我国动物制品出口屡屡被卡，给国家的经济利益造成重大损害；还有一个是今年6月陕西汉中的打狗事件，在社会上激起很大民愤,不利于社会和谐。如果《动物保护法》得以实施，这些问题就能够得到解决。”<br />
　　我深深感觉‘群众心理’是可怕的，组织的力量如果滥用也是很可怕的。……人多势众的时候往往是不讲理的。<br />
　　“我希望大家看完稿子之后再提意见，而不是道听途说后评论。目前，一些人没有看条文就提出善意的意见，可以原谅。但是一些圈子的人没有看条文，作出恶意甚至人身攻击的评论，实在不妥。由此，我想到梁实秋先生如上的评价。”<br />
　　这是常纪文博客里的最近新发的帖，发布时间是9月25日早晨7点05分。<br />
　　采访中，记者感到常纪文教授其实是一位出语锐利的学者。比如在谈到一些人的反对意见时，他会立刻反驳：“他们看这个法了吗？看都没看乱抨击什么？现在的学风太浮躁。”<br />
　　我们正着手将《动物保护法》（专家建议稿）翻译成英文，这个法案一旦在国际社会公开，常教授将面临一定的压力。<br />
　　“很多国家的NGO组织，包括欧美国家和亚洲国家，都非常关注中国专家起草的这个法案，大家可能还会对这个建议稿提出各种各样的很多的修改建议。”9月30日，英国防止虐待动物协会（RSPCA）国际计划总监兼东亚事务主管李博，在RSPCA位于望京的办公室里通过越洋电话接受了记者的采访。<br />
　　当听说《动物保护法》（专家建议稿）在中国社会面临的“超前”、“奢侈”的评价，和常纪文教授承担的各种压力和非议，李博在电话里笑着对记者说，他完全能够理解而且也早有预料。“但我们认为里面的部分内容过于保守。”<br />
　　李博预测，国际上了解中国国情的人来说，大家会认同这部法律。但是还有一些意见会认为，中国的《动物保护法》中一些内容过于保守，对某些动物的保护还不够。“比如在宠物动物的章节，对宠物的管理远远大于保护。大部分国家的《动物保护法》是不会对宠物有这样多的限制的。”<br />
　　“到时候大家可能会提出一些建议要求修改。我觉得有一些调整是可以的，但我尊重中国专家的意见，中国有自己的国情，最终应由中国的专家们作决定。”<br />
　　作为一个学者，我们是不会曲解民意的。我希望大家首先要看一下《动物保护法》，然后再发表意见，不要认为给动物立法就是侵犯人权。<br />
　　面对诸多争议，常纪文教授解释道。对于《动物保护法》（专家建议稿）的出台，国内大部分网民的直接联想就是社区内宠物的伤人事件，“有些人实在不讲公德，遛狗不拴狗链纵容宠物伤人，现在还说什么要给宠物立法保护权利，我们的人权谁来保护？这不是本末倒置吗？”<br />
　　“只要大家认真看了就不难发现，在宠物这个部分里，大量条文其实谈的是如何对宠物进行规范管理和限制，比如宠物的规范饲养，销售要求，登记制度等等。”对此常纪文教授耐心解释。<br />
　　也正因为如此，李博认为对宠物权利的保护远远不够。“大部分国家对养小猫小狗没有这么多的限制，这些内容有些严格。宠物的那个章节主要谈的就是怎么限制，怎么管理，对宠物权利的保护谈的太少太少了。好像只要把宠物带回家养就可以了，怎么养怎么保护怎么提供生活条件都没说。而经济动物的那几章写得特别好。比如一些动物是需要伴侣的或者喜欢群居生活的，饲养它们时就不能把它们与同群动物强制隔离；在运输经济动物的过程中，要保证动物的休息，提供清洁的饮用水，笼子的大小要符合标准等等，很好地体现了动物福利。我们认为，宠物也是动物，和经济动物、表演动物是一样的，应该对所有动物给予一样的保护，这才是合理的。”<br />
<br />
动物都会有渴求转身、弄干身体、起立、躺下和伸展四肢的自由。<br />
<br />
　　这是著名的动物“五大自由”概念的前身。“五大自由”的概念来自上世纪六十年代的英国。1967年，“农场动物福利咨询委员会”（1979年改组为农场动物福利委员会）提出，动物都会有渴求“转身、弄干身体、起立、躺下和伸展四肢”的自由，其后更确立动物福利的“五大自由”：免受饥饿、营养不良的自由；免于因生活环境不舒适而承受痛苦的自由；免受痛苦及伤病的自由；免受恐惧和压力的自由；表达天性的自由。目前，西方发达国家的大部分公众普遍认同“五大自由”，国际社会也将动物福利普遍理解为“五大自由”。<br />
　　《动物保护法》（专家建议稿）还在制定过程中就曾有过的激烈的内部争论，争论核心一是要不要把来自西方的动物“五大自由”写进法条，二是是否允许给动物实施安乐死。坚持方是英国防止虐待动物协会(RSPCA)和国际爱护动物基金会(IFAW)，他们也是建议稿的参与方。两个组织的国际背景，鲜明地代表了国际动物保护界的意见。<br />
　　常纪文对此观点鲜明：“对把‘五大自由’写进来我坚决反对，老百姓接受起来有困难，这不符合中国现在的国情。对这个问题我们争论了差不多一个月，合作差点终止。”<br />
　　RSPCA的中国代表邬小红对这场争论也是记忆深刻。“我记得内部人拿到草案的时间是7月末8月初，但直到9月中旬才正式对外公布。”<br />
　　我们不是非要‘五大自由’这样的字明确出现在法条里，我们只是希望能对它有一个表达，鼓励人们思考如何善待动物，从而引领整个社会慢慢进步。<br />
　　站在国际动物保护组织角度，李博认为：“‘五大自由’其实只是保证动物最基本的需要，这不是一个法律问题，而是一种精神。‘五大自由’的精神是要人们尽自己的能力去善待、照顾动物，而不是虐待动物。法律不会强制人们去满足动物的这些最基本的生活需要，这只是一种鼓励。我们当时希望的最理想情况是，‘五大自由’的精神内涵能在这部法的总则中有所体现，让整个法条里充满这种气氛。当然这只是我们的一种希望，我理解中国有中国的国情。”<br />
　　李博介绍，在大部分西方国家的《动物保护法》内，虽然没有明确出现“五大自由”这样的字，但是法律的具体条款里都会对“五大自由”的内涵有着非常详细的体现，对动物应该具有的权利给予详尽的描述。<br />
　　“从中国政府方面说大家对‘五大自由’也不陌生。中国是世界动物卫生组织（OIE）的成员国，OIE讲的动物福利普遍理解为‘五大自由’。另外，中国与欧盟签订的众多动物制品的进出口贸易协定中，规定这些动物必须是拥有动物福利的。”<br />
　　除了负刑责，谁能拿出对策阻止虐待动物的行为？有些事情恐怕连刑责都阻止不了。<br />
　　“虐待动物责任者将追究刑事责任”是《动物保护法》（专家建议稿）中最引人争论的部分。针对有网友提出，因为虐待动物就要让人负刑责，这未免太小题大做的评论，常纪文教授作出如上反问。而虐待动物情节恶劣将负刑责，此前也被媒体做成新闻大标题而广泛传播。<br />
　　“比如高跟鞋踩猫事件，你能用什么方法把它阻止？比如今年6月陕西汉中的打狗事件，当地政府为什么敢这么做？因为做这个事情没有刑责对他进行约束。”<br />
　　常教授提到的汉中打狗事件发生在3个多月前，陕西汉中发生狂犬病疫情，当地政府对疫区的几万只犬类进行集中捕杀。这种对狗不进行带毒区分就一律捕杀的行为，给宠物主人造成巨大伤害，也引起了各地舆论的强烈关注和谴责。<br />
　　RSPCA的李博认为，从国外的动物保护立法经验来看，加入刑责是世界的大趋势。<br />
　　“一些在上世纪90年代推出《动物保护法》的国家，刚开始也是没有刑责的，但是仅仅两三年之后，立法者就加入了刑责。因为大家意识到有意的虐待、遗弃动物对社会造成非常大的负面影响，必须加入刑责才能对这种恶劣的行为予以制止。所以《动物保护法》里加入刑责是世界的一个大趋势。我相信，争论到最后中国大部分的老百姓都是会接受的。比如20年前没有人会想到，中国现在也会制订《动物保护法》。”<br />
　　李博介绍，按照英国的动物保护法律，虐待动物行为恶劣最高可以判2年监禁，而中国香港地区最高可判3年监禁。<br />
　　千万不能把流浪动物交给地方政府，他们有心思管吗？要是把狗交到他们手上，指不定在哪个没人的地方把狗就给弄死了。常老师写的这个内容一定要改改。<br />
　　家住陕西汉中的张女士此前亲身经历了轰轰烈烈的打狗事件，至今心有余悸。接受记者电话采访时，张女士语带哽咽地说：“真是太惨了！我亲眼看到一个邻居在菜市场买菜，家养的小博美就放在车筐里。结果打狗队上去一把将狗拎出来，几棒子下去就弄死了。”<br />
　　“我那个邻居一下子就崩溃了。”张女士说自己家里也养了一条小狗，那段时间自己几乎足不出户，就怕回来后再也见不到自己的狗了。她说，打狗事件给汉中地区很多爱护动物的人造成了无法磨灭的心理阴影，所以身边的动物保护人士都强烈反对相关内容。<br />
　　RSPCA的李博也将“汉中打狗事件”视为“非常过分的行为”。不过，他仍然认为流浪动物的管理政府应该负主要责任，动物组织可以辅助。<br />
　　“民间的动物保护组织能力是很弱的，大部分组织成立的时间还没超过十年，还有一些地方根本就没有动物保护团体。不过民间组织可以参与到这项工作中来，负起一定的责任。”李博举例，在英国，流浪动物的管理工作由政府承担，民间组织可以协助。比如有人发现流浪狗会打电话给政府相关部门，接电话者会首先问这只狗是不是受伤的。如果没有受伤，政府会将其带回，如果是受伤动物，民间组织可以立即加入，承担医疗救助的责任，待其健康再将狗送到政府收容部门。<br />
　　人道处理绝不是安乐死，我本人坚决反对把安乐死写进《动物保护法》里。<br />
　　在《动物保护法》（专家建议稿）里，曾提到过对动物的“人道处理”，被很多国人理解为“安乐死”。对此，常教授态度鲜明地澄清：“一些西方国家的《动物保护法》确实有这个内容。但是中国人的道德观念不能接受这个，不能把西方人的那一套完全带到中国来。”<br />
　　李博也明确表态，“我也不提倡给健康动物实行安乐死，中国的《动物保护法》不需要这条，大部分人目前也接受不了。”但是他也同时提到，虽然不提倡安乐死，但是也不应该禁止兽医具有给特殊动物实施安乐死的权利。他举例，一些流浪动物被收留后发现具有很强的攻击性，根本无法驯化更无法领养，唯一的办法是关在笼子里一辈子，这对动物来说也是一件很痛苦的事情，而且常此以往收容中心也没有能力容纳这么多的动物。为了它们的生存质量，在西方国家,一些人提出，法律应该授予兽医一种权利，给这些活着确实很痛苦的动物实施安乐死的权利。<br />
　　“其实安乐死在西方国家同样是一个模糊的概念，一直没有定论。我们不提倡给动物实施安乐死，但是我们希望法律能够不禁止兽医具有实施安乐死的权利。”如果给《动物保护法》（专家建议稿）打分，站在国外动物保护组织角度，李博打7.5分，“基本OK，还有可以探讨的地方。不过比起英国1911年发布的首部《动物保护法》，中国的这个建议稿已经很好了。我很尊重和理解中国专家的工作，他们做的是一个非常复杂而且专业的工作。我将会继续旁观这部法的发展，当然我也乐意贡献我能提供的帮助。”<br />
　　目前一些追求发行量、吸引眼球的报刊，它们没有看全文，以讹传讹，就抛出一些误导社会的报道；他们的解读并没有体现我们的真意。<br />
　　“如有的媒体指出，我们的建议稿希望动物得到比人更高的福利，这完全是曲解！如我们没有设置西方国家倡导的‘五个自由’就是例证。但是，有一些条文如果超前，我们会修改。<br />
　　“有的媒体指出，动物钻火圈就得负刑事责任，这完全是他们个人的理解！有的人甚至宣传，我们鼓励对健康的动物采用安乐死，这是对‘人道处理’的误解，是完全不负责任的宣传。人道处理不等同于人道处死或者安乐死，请大家不要误解！我一贯反对对健康的动物采用安乐死！对于这些，我接受中央电视台的采访时说得很清楚。”<br />
　　面对国内部分民众“太过超前”指责甚至一些误读，在中国网的博客中，常纪文教授只能再一次的解释、澄清。而一些媒体以讹传讹的报道，又给他平添许多新的烦恼。<br />
　　常纪文认为，媒体的宣传可以将错误放大许多倍，也可以将公众的不满放大，这给《动物保护法》的顺利出台增加了新的阻力。“我还是请大家先具体看一看这部法，然后再发表意见。”<br />
　　在西安的启动仪式上，我的导师问我，他说纪文呀这个法会不会有结果？我当时坚定地说，一定会有的。<br />
　　早在研究之初，《动物保护法》（专家建议稿）的顾问也是常纪文的导师蔡守秋先生，就曾预料到即将面临的众多争议而对他有过善意的提醒。<br />
　　“《动物保护法》的发展一般有两个阶段，第一个是反虐待、反遗弃，第二个是经济水平提高之后给动物以更高福利的保护。而我们正处在第一个阶段，提高是需要一个逐步的过程的。”<br />
　　虽然困难重重又颇多争议，但常纪文觉得，这恰恰说明《动物保护法》对社会的触动很大。“现在看来它的影响比我们当初想象的要大一百倍。当初预期的时候我们是自己给自己打气，但是现在是全社会给我们打气。作为一个普通的学者，我很知足。前边的路还很曲折，我们会一直走下去。”<br />
　　发稿前，常纪文给记者发来一封邮件，上面写道：当初研究这个法的时候就是一个冷门，我根本没想到会出名。中国社科院一直就有忧国忧民的传统和求真务实的学风，所以我想我一辈子做一件实事，说几句真话，将来到老了也不会后悔。再过20年或者30年，我就已经退休了。那时候回头看，中国的动物保护立法肯定比较完善了。我可以说，我无愧于养育我的国家、社会和人民了！</div>

							
							
							
							
