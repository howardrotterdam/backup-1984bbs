																											<div id="postmessage_451859" class="t_msgfont">2010年01月26日07:33<br />
<br />
<img src="http://img.hexun.com/2010-01-26/122485262.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　我国首部“动物保护法”在征集意见过程中，有不少市民对“动物福利”提出质疑。对此，法律专家经过讨论，决定改变提法，起草“反虐待动物法”，4月份向相关部门提交专家建议稿。<br />
<br />
　　前天，动物保护法项目组负责人、首席起草专家、社科院法研所常纪文教授表示，《动物保护法（专家意见稿）》面向社会征集意见4个多月的时间里，共收到300多封电子邮件和400多个电话，大部分意见比较客观，但也有一些市民表示无法接受“动物保护”以及“动物福利”的提法。“他们觉得目前最重要的是先保障人的福利。”常教授说。<br />
<br />
　　鉴于此，项目组决定改变提法，起草“反虐待动物法”。“虐待”是指故意以残酷的手段或者方式给动物以不必要的痛苦和伤害，或者以残酷的手段或者方式杀害动物。项目组认为，“反虐待”的说法更容易被市民所接受，因此新起草的法律将贯彻反虐待的原则。目前，该法律专家建议稿已基本完成。<br />
<br />
　　常教授透露，《反虐待动物法（专家建议稿）》分为九章，大致包括反虐待动物的主要制度，野生动物、经济动物、宠物等多类动物的反虐待措施，动物医疗、动物运输、动物屠宰的反虐待措施，法律责任以及附则。<br />
<br />
　　值得关注的是，“禁食猫狗肉”写进了《反虐待动物法（专家建议稿）》。<br />
<br />
　　“禁止吃猫肉狗肉并不会给市民带来太大的影响，现在物质生活这么丰富，吃猫肉狗肉的人毕竟是少数。”常教授说。<br />
<br />
　　记者了解到，《反虐待动物法（专家建议稿）》规定，违法食用犬、猫或者销售犬、猫肉，将对个人处5000元以下罚款并处15日以下拘留，并责令具结悔过；对单位和组织处1万元以上50万元以下罚款。在监管职责方面，由公安机关统一监督管理，公安机关必须设立统一的报案电话，其他部门分工负责。<br />
<br />
　　据了解，中国土产畜产进出口总公司等支持“反虐待动物法”立法，此法出台有望消除贸易壁垒，提高我国羊绒、羽绒等产品的进出口量。<br />
<br />
　　常纪文教授表示，暂缓制定“动物保护法”，是根据现实情况所作的决定，从长远来看，“动物保护法”仍然需要。<br />
<br />
据法制晚报<br />
