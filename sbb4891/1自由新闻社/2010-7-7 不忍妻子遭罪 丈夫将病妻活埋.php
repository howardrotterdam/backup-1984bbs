													<h2>不忍妻子遭罪 丈夫将病妻活埋</h2>
						
						
																											<div id="postmessage_725938" class="t_msgfont">山西平陆：不忍妻子遭罪　糊涂丈夫将病妻活埋&nbsp;&nbsp;<br />
&nbsp;&nbsp;黄河新闻网&nbsp; &nbsp;2010-07-07 <br />
 <br />
今年40岁的赵引香曾经是平陆县张村镇东坡村数一数二的女能人。丈夫刘海洋是个忠厚人，夫妻俩原本生活得富足滋润，膝下两子也聪明懂事。然而，一场突如其来的疾病击垮了赵引香，也击垮了这个幸福的家。短短5年，刘海洋举债8万多元，全家人因而过上了赤贫的生活。最为可怕的是，赵引香失去了生的信念，几次三番自杀，旧痛未去又添新疾。于是，一场荒谬的人间悲剧上演：刘海洋将妻子活埋进了黄河滩。<br />
<br />
当个人伤痛触及社会道德底线，我们该如何面对？对于一心求死的重症病人，能否提供安乐死？不难发现，在因病致贫的个例中，社会救助体制的不完善仍是顽疾所在。<br />
<br />
糊涂酿祸 痛哭忏悔<br />
<br />
6月20日深夜，平陆县一块黄河滩涂地上，3棵果树在夜风中“簌啦啦”作响。刘海洋收起了铲和锹，放入三轮车。紧接着，他又返回原地，双脚在地上踩了几圈，又将一层土撒了上去。<br />
<br />
他愣愣地站在那里，仿佛听到妻子在土里沉重的喘息声。“她在里面‘呼哧呼哧’地出气，我一下子后悔了，想马上将她扒出来，但一想到她出来也疼得活不了，就又犹豫了。等了两分钟，听不到声音了，我才开着三轮车回了家。”<br />
<br />
回到家中，刘海洋眼前总浮现出妻子痛苦呻吟的样子，他难过得一夜一夜辗转哀叹。“我挖的坑太小，都没有把她放平，她在里面还蜷曲着身子。她跟了我这么多年，我都没有给她弄个像样点的棺材……”6月23日6时26分，难忍良心谴责，双眼布满血丝的他颤抖着拨通了“110”。<br />
<br />
民警们赶到后，刘海洋坐在炕上，神情烦躁地说，他把他老婆活埋了。民警们不相信，当即就带着他去现场寻找尸体。<br />
<br />
果真，在刘海洋的指认下，民警们在刘家的滩涂地里，挖了不到一米，就发现了赵引香的尸体。因埋得浅，尸体已经有些腐烂，民警们把随赵引香葬在一起的枕头被子一件件拣出来，细细勘查。<br />
<br />
案情震惊了全县。该县政法委书记石杜杰，县公安局局长李满庭也快速赶到了现场，督察案情。在现场，听着刘海洋断断续续的叙述，大家对这个可悲的犯罪嫌疑人，却怎么也恨不起来。<br />
<br />
回县城的路上，刘海洋仍自顾自地向民警们倾诉家庭的贫困：“我把新麦子卖了2000元钱，留给了孩子们。”说到伤心处，他号啕大哭。<br />
<br />
因病致贫 亲朋疏远<br />
<br />
1968年出生的刘海洋，1992年与比他小两岁的赵引香结婚。为了补贴家用，他白天打工，晚上抓蝎子，一天下来也有几十元收入。精明的赵引香也不甘示弱，她养了十几头猪，每天把猪舍打理得干净利索。夫妻俩辛苦一年下来，家庭收入达到了三万元开外。<br />
<br />
他们家那几孔坚固美观的窑洞，正是那个时期建起来的。当然，更让夫妻俩引以为豪的是，他们的大儿子学习成绩一直名列前茅。“在他家窑洞的墙壁上，贴满了孩子成绩优秀的奖状。”办案民警柴建刚介绍说。<br />
<br />
“他们夫妻俩的感情很好，总是和和气气地相跟着出来进去的。”一村民反映。<br />
<br />
5年前，赵引香的类风湿病发作。类风湿是以关节组织慢性炎症性病变为特点的全身性自身免疫性疾病，目前尚无特效治疗方式。晚期病人关节严重畸形、强直、功能完全丧失，患者往往不能自理，严重者卧床不起。“这种病潜伏在人体内可以长达十几年，随着病人年龄增长，抵抗力减弱而暴发。赵引香发病时，已是晚期。”平陆县公安局刑警大队指导员荆旭晨办理该案时，专门咨询过医务人员。<br />
<br />
为了给妻子看病，刘海洋耗尽了家中全部积蓄，还四处借债，能借的地方都借遍了，欠外债8万多元。<br />
<br />
随着赵引香的病情越来越严重，朋友、邻居、亲戚对刘海洋的还债能力失去了信心，他们渐渐疏远刘海洋，看见他来就赶紧躲开。<br />
<br />
2009年的一天，刘海洋再次去哥哥刘海红家借钱，吃了闭门羹。赵引香悲愤之下，拄着拐棍去把刘海红家的门窗玻璃砸了个稀巴烂，还点了一把火。屋子被烧得面目全非。这把火彻底阻断了刘海洋的兄弟情，就连刘海洋的老母亲也对赵引香有了意见。<br />
<br />
之后，平陆县人民法院依法对赵引香进行了判决，以故意损坏他人财产罪对其判三缓四。赵引香得以在家继续养病，但身体大不如前。病历显示，她在太原、西安几个大医院均有过治疗记录，病情却未见好转。<br />
<br />
“听说被蜜蜂蜇有助于治疗风湿病，刘海洋给他老婆买回一箱蜜蜂，每天蜂蜇，赵引香浑身都让蜇肿了，也没见管用。”一村民举例说明，刘海洋是个很重情义的男人。<br />
<br />
刘海洋家住在崖头，崖头下的邻居回想起去年的一件事：“我去他家喊他浇地，刚进门，就看见他老婆疼得龇牙咧嘴的，举起锅来扔到地下，摔了个稀巴烂。”<br />
<br />
赵引香想死，这几乎是东坡村人尽皆知的事。她和一些妇女拉家常时甚至说过，希望自己死后，妹妹嫁给刘海洋，照顾自己生的两个孩子长大。<br />
<br />
疼痛难忍 几次自杀<br />
<br />
“我儿子可怜啊，别人家孩子享受到的东西，他都没有享受过。”说起大儿子来，刘海洋格外伤心。<br />
<br />
大儿子理解父母的不易，回到家就抢着干农活，16岁，正是梦想飞扬的年华，他却承担了本不该承担的苦难。2009年，他考取了平陆县一中。但看到家中负债累累，他便主动提出要到职业中学去上学。职中的费用相对要低得多。去年暑假，他外出打工，一个月后还给家里带回一部分工钱。<br />
<br />
孩子们的勤俭让赵引香欣慰，却不能减轻她身体的痛苦。病情发作起来，她全身大小关节如同蚁噬，痛不欲生。“一次，我妈正要喝农药，我进去看见后，赶紧给抢了下来。”刘海洋的大儿子提供了这样一个细节。从那以后，家里的农药就藏到了赵引香看不到的地方。<br />
<br />
警方还调查到了赵引香另外两次自杀事件。<br />
<br />
今年5月8日，赵引香从地窨院4米高的土墙上跳下来自杀，因院子里的土壤较软，她没有受伤。而另外一次，系刘海洋的二儿子亲眼所见。<br />
<br />
刘海洋的二儿子今年13岁，在离家5公里的老城小学就读。这是一个瘦小的男孩，背心上满是白色汗渍，身上一条磨得发毛的裤子显然是哥哥替换下来的，长长的裤腿耷拉在脚脖子上。他低着头，很自卑的模样。案件发生后，该校老师为其做了心理辅导工作，并将他的学杂费全免，每月还补助生活费50元。“我妈经常和我们说，她不想活了。她疼起来直哼哼，把墙抓得一道道的。今年的一天，我在门口玩，听见我妈又和我爸说‘快把我弄死吧，要不给我喝农药，要不埋了我……’”孩子说话间，眼圈就红了。<br />
<br />
5月18日深夜，他睡得正香，房间里的灯突然亮了。他惊醒后，看见父亲正抱着母亲往炕上放，母亲疼得“哎呀、哎呀”直叫。后来，他才知道母亲从自己家地窨院的坡上跳下来寻死，不幸摔断了脊梁骨。<br />
<br />
这个孩子哽咽着说：“一直以来都是爸爸照顾妈妈。我爸去找姥姥她们过来，姥姥她们忙，没有来。”6月29日，距离母亲身亡仅过了9天，孩子还难以接受这个家庭变故。“我心里很不好受……”孩子一直噙着泪，转身离开时泪珠落下来了。<br />
<br />
那次自杀未遂，赵引香下肢失去知觉，连大小便都不能自理。<br />
<br />
这个时期的刘海洋，白天照顾妻子无法外出打工，一到晚上，他就提着瓶子出去抓蝎子。为了多抓点蝎子筹集治疗费，他一抓就是一个晚上。难以想象，他承受着多大的压力和痛苦。<br />
<br />
因护理不周，赵引香的腰间和臀部起了褥疮，腰间的褥疮很快溃烂成了碗大的窟窿。入夏以来，高温不退，赵引香的褥疮腐烂流脓……而刘海洋再也借不出一分钱来给她治病。<br />
<br />
妻子督促 丈夫下手<br />
<br />
尸检中，赵引香的手安详地放在胸前，现场没有任何搏斗痕迹。<br />
<br />
刘海洋复述了案发当天的情景。“她天天说不想活了，常说让我杀了她，掐死捅死都可以。我说，掐死你，我还犯法呢。她说，那你埋了我，不就行了嘛。那天，她疼得受不了，和我商量着往哪块地里埋，离我家几里远有块黄河滩地，她说她要埋到那里。我就在三轮车上铺了褥子，放上枕头，把她抱上车，不知道弄着她什么地方了，她疼得受不了，我就把家里的毛巾让她用牙咬住。给她盖了被子，拿了几件比较新的衣服开车往地里走。”<br />
<br />
到了地里，刘海洋把毛巾从妻子嘴里拿出来。他开始挖坑。挖了很长时间，才挖了一米多深、一米多宽。“她不住地催我快点挖，快点埋了她。我很害怕，我说，回哇，我带你到大医院去看病。她说，你就是给我看，我也不活了。”按照刘海洋供述，他把赵引香放进坑里。在妻子的催促下，他从妻子脚部开始盖土，由于害怕，他的动作很慢。但妻子呵斥他：“你快点吧，我受够啦！”他眼一闭，用锹铲上土，盖向妻子头部。<br />
<br />
埋完了妻子，他回家后，把毛巾洗干净，晾到绳子上。他说，还能用哩，扔了还得花钱买一块。<br />
<br />
平陆县公安局刑侦大队队长魏新军介绍：“案情仍在侦查中，只要与案情有关的可以还原案情细节的、能进行检测化验的物品，我们全部要进行化验，包括那块放进赵引香嘴中的毛巾。任何细节我们都要一再核实，以排查有无同案犯参与。”<br />
<br />
悲剧背后 几许嗟叹<br />
<br />
“赵引香吃的是农村低保，每月55元，尽管他们入了农村合作医疗，但在外地看病只能报销30%，对于他们的支出来说仍是杯水车薪。有了病大家能帮的帮，不能帮就只能靠自己，毕竟大家都不富裕。而夫妻俩的性格也逐渐变得孤僻，他们也没有去相关部门申请救助。村里因病无法生活的人也有几户，可没人想到他家会发生这样的事情……”东坡村党支部书记对记者说。“我父亲是一个瘫痪二十多年的老人，他曾经要求安乐死，作为子女苦劝无效，我们只好找遍全省医院，竟然无人敢应此事。实际上，有些病人的痛苦以及病人家属的痛苦，是别人无法体会到的。病人一心希望结束生命，解脱病痛，这种现象是存在的，社会应正视这个问题，并应该谈论这个问题的解决途径。”晋中市一位重病患者的儿子在要求不透露他名字的情况下，对此案发表了这样的看法。<br />
<br />
6月29日，当民警们告诉刘海洋，他妻子已体面下葬后，刘海洋愁云密布的脸上露出一丝欣慰。<br />
<br />
7月4日上午，东坡村刘海洋家，他的大儿子还在县城上学，二儿子坐在炕上发呆，孩子年迈的奶奶则坐在窑洞前，茫然地望着坡下的村舍……“赵引香死后，她娘家人因借钱治病的事与她有隔阂，没来参加丧事，她是村委会花钱安葬的。现在，我们担心他家老二没人收留，学校已经放假了，孩子奶奶80岁了，身体又有病，谁来照顾孩子啊？”村干部赵某忧心不已。他想通过记者呼吁一下，让孤儿院或福利院收留这个可怜的孩子。(记者 高 辉)</div>

							
							
							
							
