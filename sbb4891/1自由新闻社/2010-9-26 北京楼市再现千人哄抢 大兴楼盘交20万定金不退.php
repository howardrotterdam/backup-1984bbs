																											<div id="postmessage_844138" class="t_msgfont">北京楼市再现千人哄抢 个别楼盘交20万定金不退<br />
<br />
2010年09月25日14:17&nbsp;&nbsp;北京晚报<br />
<br />
<img src="http://i2.sinaimg.cn/hs/scan/2009-07-14/U3526P62T3D319687F263DT20090713143315.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　本报讯 20万元！这笔2007年初可以在大兴买套40平方米小居室的钱，如今却只够交个定金。昨日，大兴某项目开盘，20万元不退的定金，和平均每平方米上涨1000元的价格，依然没有吓退购房人，千人哄抢的爆棚景象再度上演。 <br />
<br />
　　20万元不退的定金<br />
<br />
　　市民陈光(化名)为了买这里的房，这几天很是纠结：他从月初开始关注，去了三次现场，排了两个号，可就在临开盘前收到通知，选房当天必须交纳20万元定金才算有效，而且将来因被拒贷而要求退房时，定金不退。<br />
<br />
　　在项目现场，记者看到购房公示写着，“开盘当日成功选房后，购房者本人交纳指定金额定金，开具定金收据，并于当日签订选中房源对应的认购书，方视为选房成功。”“客户以贷款方式支付房款的，如未在认购书约定期限内与开发公司指定的商业银行、公积金管理中心或其指定的代办机构办理贷款手续，开发公司有权随时解除认购书，客户已付定金不予退还。”<br />
<br />
　　陈光无奈地告诉记者，定金已从二期的5万涨到20万，这让购房人没了退路，即使之后再出新政策，房价跌了，也没人会愿意丢弃这么一大笔定金。<br />
<br />
　　千余人排队等房<br />
<br />
　　除了定金增加外，该项目三期的开盘均价，也从每平方米17000元，涨到了每平方米18000元，一套房子房款多出10万。可即使这样，也没有阻挡购房人的热情，1000多套房源，在开盘当天遭遇了哄抢。截至昨天上午11点，600余套房源已被“抢走”，一些没被通知选房的排号者，也赶到了现场探查情况。“只要让我选上房，20万定金，涨价一千，我都可以接受。毕竟，这个价钱想在北京买到一套地铁边的房子，太不容易了。”而且，受该楼盘的带动，旁边的一家楼盘，也在假期卖断了剩余房源。<br />
<br />
　　半数项目9月开涨<br />
<br />
　　后期上涨，并非仅这一家。北苑某楼盘，在特价将东西向房源全款卖掉后，9月将销售均价回调到每平方米25000元。而根据北京市房地产交易管理网的数据，新政前后开盘且9月依然在售的60个项目中，目前成交价超过新政前的共32个，占比高达53.3%。 <br />
<br />
　　“宏观调控政策出台后，就一直盼望着房价快降，可等了四个多月也没见大动静。如今都说成交回暖了，害怕价格涨回去，想说快出手吧，结果大家都抱着同样的想法，反而把开发商给抬牛了，担心倒成了事实。”陈光告诉记者。<br />
<br />
　　赵莹莹</div>

							
							
							
							
