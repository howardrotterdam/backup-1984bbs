<br />
世界卫生组织指，中国甲型H1N1流感死亡数字远高于官方数据。中国官方英文报章《中国日报》一篇由Shan Juan撰写的报道指，中国发布第3宗新流感死亡个案后，世卫驻中国代表Michael O'Leary称，中国官方已通报的新流感死亡案例，可能只是「冰山一角」。台湾中央社指，有海外网民发邮件提醒大陆网友，指大陆政府处理新流感和海外的情况不一样。有北京赵姓网民称，由于政府曾蒙蔽百姓，太多带有虚假成分的新闻，他们已不再相信。而官方监测数据显示，北京流感症状的人数已经超过了 6000，其中流感抽样调查显示，甲型H1N1流感病毒占33%左右，预计本周内，甲流感病例将达到全部流感病例的一半。<br />
<br />
本台记者星期四首先打北京公共卫生公益电话12320询问有关情况：记者：北京现在感染流感的人数有多少？<br />
对方：现在已经不进行统计了。<br />
<br />
记者：为什么？<br />
对方：现在常规治疗，常规进行排查就可以了，不上报了。<br />
<br />
