 来源：大洋网-广州日报 2010年04月08日<br />
[提要] 4月7日凌晨4时许，数名男子在广东佛山禅城区吃夜宵时与夜宵档人员发生纠纷，继而引发打斗。据目击者称，当时有百余人持长刀互相砍杀，其中还有人用自制霰弹枪开火。警方昨日通报证实打斗造成两人受伤，其中一人为枪伤。受枪伤者称，当晚他和老乡路经打斗附近，吓得逃跑过程中背部遭枪击中，而老乡则伤在腹部…[我来说两句] <br />
 <br />
警方正在事发现场进行勘查。 <br />
<br />
<br />
&nbsp; &nbsp; 同城报道：广东佛山发生枪战 目击者称百余人员用长刀互砍<br />
<br />
　　消夜起冲突店员顾客打斗开2枪<br />
<br />
　　现场观察：地面满是血迹 汽车被砸<br />
<br />
　　事发广东佛山禅城区高基街口仿古街一店铺门前。昨日上午10时许，警方在现场拉起警戒线，“锦标号商行”、“爽香食坊”和“万州特色烤鱼王”3家店铺被拦于警戒线内。其中商行的玻璃门被击得粉碎，门口满是玻璃碴，多名警察走进走出调查取证，铁闸门被拉下一半。<br />
<br />
　　店铺门前的桌椅上、地面上，多处条带状血迹隐约可见。警方在上面放置多个坐标物进行标识，两名民警对血迹进行取样。距事发现场不远处，一辆黑色轿车泊于路边，轿车四周玻璃已被人全部打碎，右倒后镜折断，轮胎全部干瘪。警方证实，该车为双方打斗中被砸损。<br />
<br />
　　直至昨日下午2时许现场解封，从商行半开的门外向内望去，一楼玻璃柜台及物品大多完好无损，2名民警走入商行2楼调查。商行老板娘称，2楼是店中的办公场所，平时偶尔有朋友在此聊天。她昨日凌晨2时许离开店铺时店中并无异样，但一早便接到员工通知称“店被砸了”。她回忆，大约半个月以前，店铺的铁闸门上曾被人泼了红油漆，之后店铺门口还加装了摄像头。<br />
<br />
　　男子背部中弹<br />
<br />
　　事发凌晨4时许，附近少人经过目睹事件。一目击者称，当时他听到类似于水管等铁器的击打声，并夹杂着打斗嘶吼声，共持续5分钟左右。其间他听到两声“放炮”声，直到第二声“炮”后，嘶喊、击打声才逐渐消失。<br />
<br />
　　事后2名伤者被送至佛山市中医院。其中一人背部和臀部被霰弹枪击中昏迷，醒来后需打镇痛剂止痛，另一人腹部受伤。医生表示，二人伤势尚在观察阶段，接下来可能需要做手术。<br />
<br />
　　其中一名伤者赵辉称，他来自河南周口，来到佛山只有十几天时间，目前尚无工作。事发当晚他和另外4名老乡一起消夜。饭局结束大家分头离去，他和另一名老乡走到仿古街红绿灯附近时，发现有十几个人正在打架，他俩吓得马上跑开。逃跑过程中他背部被霰弹枪击中，但对于为何另一同伴同他一起逃跑、却是腹部受伤，他无法作出解释。<br />
<br />
　　警方：不是误伤是打斗<br />
<br />
　　禅城警方昨晚通报，4月7日凌晨4时许，数名男子在禅城区高基街口吃夜宵时与夜宵档人员发生纠纷，继而引发打斗；共有两人受伤，其中一人为枪伤。案件发生后，禅城公安分局永安派出所民警迅速到场处置，将两名伤者送往医院救治，并将双方涉案人员带回派出所调查。民警在现场缴获自制霰弹枪一支和刀具，现伤者正在医院接受治疗，案件仍在进一步审理中。<br />
<br />
