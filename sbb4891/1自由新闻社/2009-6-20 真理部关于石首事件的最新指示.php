&nbsp; &nbsp; 6月19日上午9时，湖北石首市汽运公司联合消防、交警、医疗等部门举办了一次公交车火灾事故处置演习，市政府相关领导现场观摩了此次演习。<br />
<br />
&nbsp; &nbsp; 当日上午9时05分，石首市消防部门接到报警称：市汽运公司院内一辆公交车失火，火势燃烧迅猛，无法控制，请求消防队紧急救援。接到报警后，该市消防部门立即调集1台消防车、8名指战员赶赴事故现场。5分钟后，参战力量到达现场，此时，汽运公司院内布满了数百名围观群众，交警部门在四周设置了警戒带，防止无关人员进入，120急救车则停靠在一旁。消防官兵在汽运公司安全责任人的介绍下了解到，失火的是一辆公交车，车内无人员乘坐，车内座位均被大火笼罩，失火原因是因电线路段而致。 <br />
<br />
&nbsp; &nbsp; 2分钟后，消防官兵迅速利用2支消防水枪从公交车前后扑救大火。5分钟后，大火被彻底扑灭，演习圆满结束。(通讯员 王方）<br />
