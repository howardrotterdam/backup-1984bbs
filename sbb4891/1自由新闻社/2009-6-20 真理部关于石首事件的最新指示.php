																											<div id="postmessage_132298" class="t_msgfont">湖北石首市发生命案【政府杀人抢尸毁证】7万人 暴乱<br />
本帖最後由 ＷＥＬＫＩＮ 於 2009-6-21 04:25  <br />
<br />
<br />
政府杀人抢尸毁证。官方竟然愚民为演习<a href="http://news.xinhuanet.com/politics/2009-06/20/content_11572718.htm" target="_blank">http://news.xinhuanet.com/politi ... ontent_11572718.htm</a> <br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
2009年6月17日晚7：30左右，湖北石首市永隆大酒店发生一起命案，该酒店23岁的男性厨师涂远高（石首市高基庙镇长河村人，此前报道中误称为女性，特此致歉）从三楼掉落，当场死亡，目击者报案后，警察到现场看后说是自杀，但家属认为涂远高死因可疑，因为涂远高耳鼻内有干血块，但尸体落下的地方却无一点血迹，而且这家酒店两年前发生过一起同样的命案，一女性服务员以同样的方式死于非命，酒店赔偿3万元后不了了之，还有人称，该酒店1999年也发生过一起类似事件，一女性怀疑被强奸后扔下楼。据说该酒店有石首某领导参股，酒楼生意一直很差，主要靠贩毒维持经营，石首市有吸毒人员愿意出面作证该酒店专事贩毒。<br />
<br />
<br />
<br />
<br />
事发后，酒店老板对涂远高家属避而不见，死者家属无法联系到酒店老板，6月18日，死者家属得到答复，若承认自杀，可得到3万5千元赔偿，如不承认自杀，当晚八点尸体将被强制性送殡仪馆火化。事发后，警察也一再催促涂远高家属将尸体拖走火化，并且有人阻止殡仪馆向死者家属出租冰柜，目前当地气温多数时间在30度以上，死者尸体被放置在酒店内，涂远高家属坚持在疑点被解开前拒绝尸体火化。其父在酒店一楼放置了液化气罐，要与抢尸者同归于尽，同情者也开始聚集在酒店门口。<br />
<br />
<br />
<br />
<br />
2009年6月19日凌晨1点左右，警车和殡仪馆车辆到达酒店现场，想把尸体运走，被现场两千名民众堵住酒店门口阻止。6月19日上午8点，涂远高死亡时间已超过36小时，愤愤不平的民众依然没有散去，几百名民石首市民自发为死者守了整整一夜，8点过后酒店门口被同情者悬挂上大字条幅和民众的签名信。不久，警察再次来到现场，要强行运走尸体，与现场民众发生冲突，十几名同情者和围观者被打伤，但这些人被打伤后不仅没有被送往医院，反而被警察直接关进看守所。这导致了民众与警方情绪对立的升级，越来越的的民众赶往现场，凭借人数的优势，民众在冲突中开始占据上风，他们一次次将警察打退，涂远高尸体始终未被抢走。下午1点半左右，现场几千名民众用砖头、啤酒瓶阻止警察抢尸，殡葬车被砸，几十名着装警察和便衣被民众从事发现场（永隆大酒店）追赶约500米逃至车站躲藏，车站附近主要交通道口被群众堵塞，公交车被迫改道，但据说绝大多数乘客毫无怨言。<br />
<br />
<br />
<br />
<br />
6月19日下午3时，当地警方组织的最大一次抢尸行动再次失败，大约4万名市民聚集在街头，人数达到最高值，石首市政府被迫向武警求助，6月20日傍晚，从荆州等地调来的约一个营的武警赶到，当地民众又用石块、酒瓶与武警展开对抗，许多公安、武警车辆被民众砸毁。由于民众大量聚集，武警被迫撤退。<br />
<br />
<br />
<br />
<br />
6月19日夜，由于传言政府将在6月20日凌晨5点再次抢尸，上万名石首市民连夜上街，不仅将永隆大酒店门口围住，还堵死了该市主要路口。<br />
<br />
<br />
<br />
<br />
6月20日凌晨两点左右，500名左右的警察及武警排队前往酒店抢尸，刚走到附近的防疫站，就被几千名市民用砖头和石块攻击，警察和武警再次撤离，民众追赶约一公里，有警察被掀翻。武警的撤离并未让民众掉以轻心，很多人继续自发守护在酒店门口。在19日及20日凌晨的冲突中，共有几十名市民受伤或被抓，警察和武警也有多人受伤。<br />
<br />
<br />
<br />
<br />
为控制事件信息的传播，6月20日夜间，石首市区网吧断网，永隆大酒店一带一度断电，路灯也被熄灭。据了解，事发后死者家属及当地民众曾给北京及武汉的新闻单位打电话，但赶到石首的记者被当地官员劝回。据称19日石首市市委召开紧急会议，不准各机关事业单位人员到现场观看。<br />
<br />
<br />
<br />
<br />
到6月20日早晨6点，涂远高尸体仍放在酒店内，未被抢走，现场仍有上千人堵住酒店门口，随着天色转亮，有市民陆续赶往现场。七点左右，现场人员用手机短信传回消息说，大批武警乘坐大巴车再次赶往现场，同时还有至少八辆防暴装甲车和六辆消防高压水枪车一起前往，再一次的冲突无法避免，石首民众呼吁外界给与关注和声援</div>

							
							
							
							
