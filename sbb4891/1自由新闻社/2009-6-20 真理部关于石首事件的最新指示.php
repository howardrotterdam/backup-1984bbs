&nbsp; &nbsp; 20日下午，围观群众聚集在被烧毁的永隆大酒店门前的道路上。 本报记者　占才强　摄<br />
<br />
<br />
&nbsp; &nbsp; 20日晚，死者家属在酒店一楼的一间内室里守护冰棺中的尸体。<br />
<br />
<br />
&nbsp; &nbsp; 警方出示的死者留下的遗书。<br />
&nbsp; &nbsp; 本报讯 17日至20日，湖北省石首市发生了因酒店厨师非正常死亡而导致数万群众围观、道路被堵塞事件。21日凌晨，事态逐渐平息，停放在石首永隆大酒店内的一具男尸被抬上殡仪车，送往殡仪馆，围观人群全部散去。<br />
<br />
&nbsp; &nbsp; 家属拒绝解剖尸体<br />
<br />
&nbsp; &nbsp; 17日晚8时许，石首市公安局接到报警，称该市永隆大酒店门前发现一具男尸。接警后，民警迅速赶往现场勘查。<br />
<br />
&nbsp; &nbsp; 经查，死者涂远高，男，24岁，系石首市高基庙镇长河村人，生前为永隆大酒店厨师。警方在对死者所住房间进行检查后，发现死者留下一封遗书，大致内容是自己因悲观厌世而轻生。警方排除他杀，初步认定为自杀。民警多次与死者亲属进行沟通，提出为了进一步查清死因，必须进行尸体解剖，但死者亲属表示对死因有质疑，将尸体停放在酒店大厅，引来众多的围观群众。19日，一些人在该市两大交通要道东岳山路和东方大道设置路障，阻碍交通，现场秩序出现混乱。<br />
<br />
&nbsp; &nbsp; 不法分子纵火滋事<br />
<br />
&nbsp; &nbsp; 20日凌晨，事态开始恶化。少数不法分子借机制造事端，在停放尸体的酒店内纵火滋事，并煽动一些围观群众，袭击前来灭火的消防战士和公安民警，导致多名警察受伤，多部消防车辆和警车被砸坏。<br />
<br />
&nbsp; &nbsp; 事件发生后，中央领导同志高度重视，对处理事件作出明确批示。公安部、武警总部、省及荆州市的主要负责人迅速组成了事件处置领导小组，一方面全力做好死者亲属的安抚工作，保护他们的安全，通过多种途径及时向社会公布事件真相；另一方面，加强对事发地段的警戒，防止事态扩大，疏散围观群众，维护现场秩序。经多次协商，死者亲属同意将尸体运往殡仪馆，将进行尸检。<br />
<br />
&nbsp; &nbsp; 目前，涂远高的死因正在进一步调查中，石首市的秩序已经恢复正常。　　《楚天金报》供稿<br />
<br />
&nbsp; &nbsp; 现场<br />
<br />
&nbsp; &nbsp; 家属“护尸”80小时<br />
<br />
&nbsp; &nbsp; 6月17日晚8时许，永隆大酒店对面，63岁的小卖部老板余昌华听到了24岁的酒店厨师涂远高坠楼后发出的“嘭”的声音，他闻声赶到坠楼现场，看到死者躺在酒店门前的人行道上，仰面朝天。“现场没有一点血迹，尸体很干净。”围观者有七八人，有人摸了死者“身上是冰的”。他们的直觉是“很像是坠楼之前就已经死了”。<br />
<br />
&nbsp; &nbsp; 随后赶到的警方初步认定是自杀，并要求将尸体带走解剖，遭到死者家属的拒绝。一起简单的死亡事件，引发了一场长达80小时的“护尸”行动。<br />
<br />
&nbsp; &nbsp; 家属的质疑<br />
<br />
&nbsp; &nbsp; 涂远高被发现死亡后，接警的警察和家属都赶到了现场。警察看了现场后，初步认定为跳楼自杀。这让家属们满腹疑惑：跳楼自杀，为什么躺的地方却很干净，一点血迹没有？而且凭他们对死者的了解，没有任何自杀的征兆。<br />
<br />
&nbsp; &nbsp; 现场处理约40分钟后，警方通知的殡葬车准备将死者尸体拖走，被闻讯陆续而来的死者家属们坚决制止。<br />
<br />
&nbsp; &nbsp; 这期间永隆大酒店大门紧锁，里面无人。家属试图联系酒店的负责人，却无法接通。气愤的家属破门而入，把尸体抬进了酒店大厅。<br />
<br />
&nbsp; &nbsp; 在仔细查验尸体后，家属声称，发现死者颈部有被掐过的痕迹，下身生殖器也怀疑被脚踢过，一侧阴囊浮肿，另外胸部还有一处较为明显的伤痕。<br />
<br />
&nbsp; &nbsp; 第一次谈判<br />
<br />
&nbsp; &nbsp; 酒店门前，附近的市民闻风而来，众多群众开始围观。<br />
<br />
&nbsp; &nbsp; 18日凌晨1时多，市公安局派人要求将尸体运至殡仪馆，遭家属再度拒绝。由于石首气温较高，家属找到笔架山派出所说理，后从市殡仪馆调来了一个冰棺存放尸体。<br />
<br />
&nbsp; &nbsp; 上午9时多，市公安局再次派人前来，和家属在酒店的客房部作了一次谈判。据来自警方的消息，民警对死者在酒店的房间曾进行过检查，发现了死者留有一封遗书，大致内容为悲观厌世而轻生，警方亦由此排除他杀，初步认定是自杀。警方说，要查清死因，要运走尸体做进一步解剖，但谈判没有结果。<br />
<br />
&nbsp; &nbsp; 家属们对死者留下遗书表示质疑。“三喜(死者的小名)13岁就出来学艺，小学3年级的文化程度，凭我们对他的了解，他怎么可能写出遗书来呢？”<br />
<br />
&nbsp; &nbsp; 谈判失败，现场情况进一步恶化。众多不明事由的群众参与其中，在东岳山路和东方大道纷纷设起路障，交通几近瘫痪。永隆大酒店门前的东岳山路，现场围观的群众已超过千人。群众把酒店大门用椅子堵住，阻止他们担心的”抢尸，强行火化”。次日凌晨零时30分和3点多，一拨身着制服的警察试图进入酒店，但被围观的人群拦在外面。<br />
<br />
&nbsp; &nbsp; 数万人围观<br />
<br />
&nbsp; &nbsp; 19日上午，高基庙镇领导和派出所出面，再次找到死者家属。家属称，镇领导协商时提出，让家属签字承认是自杀，然后对方先出3.5万元安葬费，以后的钱以后再说。家属们回应，我们不要钱，我们就是要给个交待，人是怎么死的。<br />
<br />
&nbsp; &nbsp; 再次协商无果。下午2点多，赶来增援的公安和武警开进东岳山路，目击群众说“最少20辆警车”。“保护尸体！保护证据！”人群中不断有人高喊，现场部分群众拿起砖头、啤酒瓶等，阻止警方进入酒店。高基庙派出所的一辆警车被砸翻。<br />
<br />
&nbsp; &nbsp; 20日凌晨2点，武警开着消防车再次试图进入，人群将轮胎砸穿，有七八名武警受伤。此时，据当晚在场群众描述，从皇叔街一直到笔架中学，1300米的街道，至少聚集了2万多名群众，达到围观的高峰。事态的发展，早已超出家属所能控制的范围。<br />
<br />
&nbsp; &nbsp; 20日下午，一处大火从永隆大酒店的一楼点燃，目击者称是一个年轻人。火势渐次汹涌，并烧向二楼、三楼。由于火势较大，死者父亲涂德明、母亲陈桂香被烟熏呛，下午5点多被送往医院。<br />
<br />
&nbsp; &nbsp; 20日下午，死者表弟王子豪把自己和冰棺关进酒店大厅内间的一个小屋，备上了两桶汽油，准备随时同归于尽。<br />
<br />
&nbsp; &nbsp; 20日晚，群众被限制进入现场，21日凌晨，大量武警和公安开始进驻。经有关人员再次与家属协商，终于在21日凌晨5时许，疲惫的死者家属终于同意将尸体运往殡仪馆。<br />
<br />
&nbsp; &nbsp; 此时，距事发时间已超过80小时。<br />
<br />
&nbsp; &nbsp; 死者的遗书<br />
<br />
&nbsp; &nbsp; 21日下午，死者家属在尸检委托书上签了字。据了解，此次鉴定由省级法医部门进行，结果有望很快公布。<br />
<br />
&nbsp; &nbsp; 家属们也第一次看到了死者的遗书。这是一封在电信缴费单上书写的简短遗书，潦草的字迹这样写道：<br />
<br />
&nbsp; &nbsp; ”亲爱的爸爸妈妈，儿子在这里对您们说声不孝了。我也不知道我是怎么了，好像有个阴影一直缠着我不放，可能这是我的命吧。我存的那点钱您们拿去用，就当是我对您二老的一点小小补偿吧，儿子亏欠您的养育之恩只能来世再报了。还有哥，我们只能来世做兄弟了，爸爸妈妈就交给你了。请原谅弟弟这样不辞而别。希望你好好把事业做大。好了就这样吧。不孝儿子，在此对父母叩头。”<br />
<br />
&nbsp; &nbsp; 遗书没有落款。<br />
<br />
