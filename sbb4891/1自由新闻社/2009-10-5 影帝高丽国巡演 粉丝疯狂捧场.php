<br />
　　温JB从机场直接前往锦绣山纪念宫，瞻仰金日成主席遗容，并敬献花篮。温JB在留言簿上题词：“深切缅怀朝鲜人民的伟大领袖、中国人民的亲密朋友金日成主席！”<br />
<br />
　　访问期间，温JB将同朝鲜领导人举行会谈、会见，就中朝关系及共同关心的问题交换意见，并出席庆祝中朝建交60周年暨中朝友好年有关活动。温JB还将赴桧仓中国人民志愿军烈士陵园凭吊。<br />
