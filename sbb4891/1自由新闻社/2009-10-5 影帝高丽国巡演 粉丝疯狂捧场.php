 　　当地时间10时30分许，温JB乘坐的专机降落在平壤顺安国际机场。朝鲜劳动党总书记、国防委员会委员长金正日，朝鲜最高人民会议常任委员会委员长金永南，朝鲜内阁总理金英日到机场迎接。温JB走下飞机舷梯，与金正日、金永南、金英日热烈握手拥抱，互致问候。<br />
<br />
　　在机场举行了隆重的欢迎仪式。朝鲜人民军军乐队奏中朝两国国歌，鸣礼炮21响。温JB在金英日陪同下检阅朝鲜人民军仪仗队。中国驻朝鲜大使刘晓明及中国驻朝使馆工作人员，在朝中资机构、华侨、留学生代表，朝鲜各界群众也到机场迎接。<br />
