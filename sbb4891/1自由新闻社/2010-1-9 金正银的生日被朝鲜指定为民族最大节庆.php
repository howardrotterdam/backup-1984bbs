																											<div id="postmessage_422525" class="t_msgfont">韩联社首尔1月8日电 有报道称，1月8日是朝鲜接班人金正银的生日，这一天被朝鲜指定为民俗最大节庆。<br />
<br />
每日NK 8日援引朝鲜平安北道的消息报道说：“由党政军高干参加的中央报告会，7日在平壤举行。”“该报告会发表了‘能拥护青年大将军金正银同志为我们的领导，是我们民族最大的幸运。所以我们决定将金正银的生日指定为民族最大的节庆。’”<br />
<br />
报道还说：“（朝鲜当局）将7日下午和8日定为民族最大的节庆，并向各地组织转达了这一新的国定假日。”“朝鲜军部、企业和合同农场等，8日上午分别举行了‘宣誓效忠’和‘唱效忠歌曲’的活动。”<br />
<br />
但是，统一部表示：“开城工业园区的员工在8日当天没有异常行动，照常进行了工作。”“在金日成诞辰的4月15日和金正日国防委员长生日的2月16日，朝鲜员工一般都不上班。”<br />
<br />
该部还说：“若是国定假日，朝鲜的电视会从上午开始播放。但是今天照常在下午5点才开始播放。”（完）<br />
