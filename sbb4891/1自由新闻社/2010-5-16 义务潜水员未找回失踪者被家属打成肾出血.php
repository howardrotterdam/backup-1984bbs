--------------------------------------------------------------------------------------<br />
<br />
2010年5月14日上午，浙江台州市政法委副书记翁于挺在温岭市副市长陈刚和温岭市公安局副局长陈继光的陪同下来到温岭市第四人民医院慰问救助失事渔民反被打的全国道德模范郭文标，翁于挺要求温岭相关部门严肃查处打人者。<br />
<br />
5月7日深夜10点多钟，浙岭渔23594号渔船在距温岭市大约120海里东海海域被撞倾覆，船上7人与家中失去联系。8日下午，接到石塘镇政府关于渔船失踪的消息后，温岭市政府第一时间成立了事故处理领导小组。从11日中午12点半开始，由郭文标等人组成的搜救队，对被撞沉的“浙岭渔23594”渔船进行全面排摸。经过近2个小时的水下搜救，搜救队找到6名失踪船员，而船长赵美桂的尸体却毫无踪迹。随后，赵美桂家属坚持要搜救队继续搜索，郭文标表示已经找遍了也没找着。返岸时，郭文标遭到了赵美桂远房亲戚的殴打，最终被送入医院进行治疗。<br />
<br />
5月13日，经温岭市第四人民医院鉴定，郭文标伤势为头部血肿，颈部软组织挫伤，腰部软组织挫伤等。但在经过这两天连续治疗后，目前，郭文标身体病情趋向稳定。只是心脏和肾功能有点异常，正在进一步检查治疗和复查阶段。<br />
<br />
救人的全国道德模范郭文标被打后，温岭和台州各级领导都高度重视。5月13日，温岭市市长周先苗专程到医院亲切看望郭文标，嘱托医院给予最好的治疗。并要求公安机关从快从严查处此案<br />
<br />
“郭文标被打事件发生后，我们局很重视，专门组织治安、派出所相关警力，及时开展调查取证。”温岭市公安局副局长赵继光告诉记者：通过温岭公安部门的努力，到目前为止，已经有3人归案。现在正在对整个案件进行进一步侦查之中。<br />
<br />
被人无辜殴打后，郭文标虽然有满肚子委屈，但他表示救人还会继续下去。“被打归被打，救有归救人。不可能说今天把我打了，以后就不去救人了。”郭文标说。只要他伤好有一点能力，照样还是会去救人。<br />
