																											<div id="postmessage_629743" class="t_msgfont">就你会copy吗？<br />
<br />
<br />
<br />
支那<br />
维基百科，自由的百科全书<br />
支那（梵文：cina），亦作至那、脂那等，乃是梵文佛经中的一个词汇，意指中国，支那原为隋唐时佛教僧侣的一种汉字音译，一般出现在汉译佛教经典中。<br />
这一汉字词汇于9世纪初通过佛教交流传入日本。18世纪初，日本学者在学习兰学（荷兰学）时，将“支那”一词与西方词汇“China”对应起来，作为其在日语中的梵音译词。（China英音译词则为チャイナ）<br />
明治维新后，“脱亚入欧”的思潮在日本日益兴盛，“支那”一词开始在日语中大面积地替换原有对中国的各种称呼。在日语中，该词的汉字写法与中文一致：支那，假名为“シナ”，罗马字拼写法一般有两种：“shina”（平文式罗马字）或“sina”（训令式罗马字）。<br />
甲午战争令日本开始跻身东亚地区强权的行列，而中国的国力则进一步衰弱。在1912年宣统帝退位，民国成立，中国有了一个新的正式称呼“中华民国”，但“中华民国”这个国号并没有立即得到世界各国的承认。清王朝倒台后，中国发生了内乱，各省独立，南方的革命党在金陵成立了一个叫中华民国的临时政府，但那时中华民国的范围只限于南方数省，北方的大片土地并不属于中华民国。此时清王朝倒台，已不能称中国为“清国”了；而称中华民国也不合适，一是因为那时中华民国还不能代表中国全国（南北和谈后北方才承认中华民国的国号）；二是因为日本政府一段时间内并没有正式承认中华民国。在此情况下，日本政府放弃“清国”的称呼转而用民间惯用的“支那”一词称呼中国。1913年7月日本政府明文规定：今后不论中国的国号如何变化，日本均以 “支那”称呼中国。1930年代时，日本已决定在外交正式场合弃用，但是“支那”一词依然为日本上下广泛使用。直至第二次世界大战日本战败之后，“支那”一词才从日语的正式场合中全面退出。<br />
如今，中日等国的民族主义者对使用该词是否在带有敌意上存在争议，一般的共识是：以个人身份屡次地或者在正式场合使用“支那”称呼中国，或称呼中国人为“支那人”，是一种不礼貌、不尊重中国的行为。<br />
<br />
“支那”在日本用法<br />
<br />
[编辑]清亡之前<br />
而日本在江户幕府末年明治维新初年也开始使用支那一词来称呼中国。由于过去日本以“汉”、“唐”、“明”等方式称呼都是中国朝代的名称，为了确定对中国的称呼，便使用支那来称呼中国。一如维新志士高杉晋作的汉诗“单身尝到支那邦　火舰飞走大东洋　交语汉鞑与英佛　欲舍我短学彼长 ”，文中支那非但不含贬意，反有钦羡之情。当时日本政府在正式场合把中国称为“大清国”或“大清帝国”，比如把甲午战争称为“日清战争”。在一般的民间报刊，则多把中国称为“支那”，把甲午战争称为“日支战争”。<br />
而中国当时的留学生，尤其是反对清朝统治的革命家们，也以支那标志出身国。1902年，章太炎等在日本东京发起《支那亡国二百四十二年纪念会》。1904年，宋教仁在东京创办了名叫《二十世纪之支那》的杂志。另如孙中山的革命伙伴，日本人梅屋庄吉，在辛亥革命成功后在日本发起成立“支那共和国公认期成同盟会”（此一历史在广州黄埔军校的孙中山纪念馆内有记载，该期成同盟会的匾额亦可见于该地），敦促日本政府承认中华民国，此时的支那也显然没有侮辱之意。<br />
[编辑]二战期间<br />
1911年中华民国成立之后，由于中国没有完全统一，处于军阀割据之下，1913年，7月日本政府明文规定：今后不论中国的国号如何变化，日本均以“支那”称呼中国。日本不用“中国”的另一个原因是“中国”这个词明示这里才是中央之国，这样日本就成了“东夷”。<br />
1919年五四运动之际，一些中国爱国志士上书政府，要求日本不得使用“支那”或“支那共和国”的说法。中国政府与日方交涉未果。著名诗人闻一多在其1925年诗歌《我是中国人》中自称“我是中国人，我是支那人”[1]。1932年日本官方在中华民国政府的要求之下，改以“中华民国”代替支那在官方文书的称呼，但民间报刊仍称中国为“支那”。<br />
1937年发生七七事变，中日爆发全面战争。日方把七七事变叫做“支那事变”。整个抗日战争期间，日本官方也把中国叫做支那，以示对抵抗中的国民政府的不承认。1946年在第二次世界大战战败后，日本政府向全国发出《关于回避使用支那称呼之事宜》的通告[2]，此后“支那”这个词完全从日本政府的公文、教科书、报刊杂志中消失。<br />
由于第二次世界大战期间，日本以支那称呼中国，所以中国人将“支那”视为对中国的一种蔑称。由于“支那”是蔑称，于右任先生主张将“印度支那”（印度和中国之间的一个半岛）称为“中南半岛”。著名作家郁达夫在《沉沦》中将他对“支那人”一词给他的侮辱感进行了深切的描述。<br />
[编辑]二战之后<br />
日本战败后完全接受中国的条件，官方在公开场合不再使用“支那”一词。然而现在，一些日本的右翼分子，如东京都知事石原慎太郎就在公开场合说“支那人”。<br />
支那一词在日语中虽不再用于称呼中国，但并没有彻底消失。如拉面又称“支那そば”（そば是日本的荞麦面条），或称东海为“|シナ海”（即‘东支那海’的片假名写法，但书写时一律用片假名，避开令人不快的汉字。）。另外在关于二次大战的话题上也会使用“支那”一词。<br />
在中文里“支那”一词也还由于历史关系没有被彻底淘汰，如地理名称“印度支那”。<br />
[编辑]支那人<br />
在江户时代中期以后日本开始改对中国人称呼为支那人；从甲午战争开始，因为侵略的关系增加的敌我意识，这词开始有蔑称的意思。第二次世界大战后日本政府接受中华民国政府的要求，媒体、教科书、公文、公务员对支那的称呼都禁止。<br />
当今支那人在日语中是指中国境内（汉人）血统的人民（不包括蒙古、新疆、西藏等地）实际上，在第二次世界大战期间，日本语中的支那是指整个谋求占领的中国，那时候说的支那人，为中国领土之中国人，而在战时海外华人如英属马来亚、新加坡、荷属东印度之华人亦被囊括在内。<br />
现在这个用语在日本正式的场合已经不再使用，但日本激进右翼人士仍用支那人称呼中国人以表示蔑视。在匿名论坛（如：著名的2ch）用支那人称呼中国人的人仍有不少。<br />
<br />
是否蔑称的争议<br />
<br />
目前，大部分中国人认为“支那”是一个蔑称。但是，也有人认为将“支那”视为蔑称是一种误解。他们认为正如英语中 China 不是蔑称一样，支那只是对中国这一个地域不带“中央帝国”（Central Empire）色彩的客观称呼，或只是一个轻蔑性不高的称呼（实际上冒犯性比以倭寇称呼日本人低许多）。[3]<br />
然而问题在于，大多数人认为一个称呼在听者耳里为蔑称，即赋予了其相应的感情色彩，那就是蔑称。譬如：非裔美国人（即美国黑人）中，很多在种族上属尼格罗人种，但大多数人认为称呼黑人是“尼格罗（en:Negro）”是蔑称。再如英语中 en:Chinaman （中国佬）从字面上是“中国人”的意思，但中国人认为有贬低的意思。或者日本自称的 やまと 的汉字写法可以是“大和”，也可作“倭”，但现代中文中称呼日本人为“倭”或“倭人”往往有轻蔑的意思。<br />
故现在大致上公认称呼中国为支那是带有侮辱性的称呼，不宜使用。与其相对的，鬼子、小日本、倭寇等词亦具有侮辱性，同样不宜使用。</div>

							
							
							
							
