																											<div id="postmessage_627271" class="t_msgfont">[支那」是梵文「秦」读音的汉译，是秦时代印度对中原国的称呼。由去印度取经的中原国僧侣带回国，作为外来语写在经文上。而来到唐朝长安游学的日本空海弘法大师（778-835）等人，移植了这个新词到日本。<br />
<br />
空海在他的诗集《性灵集》中写下「支那台岳曼殊庐」诗句，这可能是日本，最早用支那称汉土的文字。平安末期（1156-1185）成书《今昔物语集》出现过「支那国」的名号。同时期（1106年）的《东大寺要录》上也有「印度支那未尝见闻矣」的话语。以上是天生日本人老古板，所学习到的常识。<br />
<br />
由于这些只是学者的读物，日本一般民众并不接触，而且绝大多数作者，照例用「汉土」和「中华」指称中原国的事物，习惯随各朝代称汉土，为唐、宋、元、明等，所以「支那」一词罕见于日语之中。<br />
<br />
直到江户时代一七一三年，罗马人约翰，漂流到日本，学者新井白石奉命接待，他倾听约翰讲述世界的状况，并据此写了一本书《采览异言》，是一本讲解世界地理的书。他根据，约翰对中原国的发音「china」注上「支那」，从此开了日本实用「支那」的先河。<br />
<br />
与此同时漂流到美国的日本人，也从美国人那里学来「汉土」的新称呼，他们在写下「支那」时还特意加上「就是唐、唐土、唐山」的注解，可见当时大多数日本人还不熟悉「支那」这个词。<br />
<br />
这样始皇帝的「秦」，传入印度后变成了「支那」，一路逆流回中原国再输出日本，另一路绕过欧美引进日本，由此在日本生下了根。<br />
<br />
到了明治维新后的次年（1869），歌舞伎剧脚本作者默阿弥在作品《忘拿的乐》中，频繁用「支那」和「支那唐山」，使「支那」很快流传起来，并成为被广泛应用的日语。<br />
<br />
明治时代出身的文学工作者们，就在这种语感下长大。其时，一直属于日本国文学范畴的汉学，被从新界定，其中绝大部分内容独立出来，「回归」它应处位置，就被命名为「支那学」。<br />
<br />
此后，许多受「支那学」熏陶的日本学人对「支那」一往情深。诗人夏目耿之介（1890-1971）曾在《唐山感情集》的「叙」中这样写道：「对我来说，支那这两个字本身就令我无条件地敬重酷爱…，支那就是china，支那两字强烈地，充满世界一流文化国家的传统和历史的神韵。」<br />
<br />
日本人本来称呼中国，通常是随着朝代叫&lt;唐、宋、元、明、清&gt;之类。汉人自己和附近国家都是这样叫，以前没有人叫「中国」，到了近代，才学美国人改叫「支那」，本来也没什么轻蔑意思。不过，后来中国成了日本手下败将，又是侵略的对象，「支那」一辞，才被无聊赖学者，影射是看不起中国之语言，而叫喊起来。<br />
<br />
于今，在多数中国人对「支那」一词反感，也有政客将「支那」视为蔑称。都是出自极端派『中国民粹』政客，哗众取宠之造势手段也。是汉人打败「清朝」，建立「中华民国」自称「中国人」以后，才变成政客哗众取宠，造势之上乘手段也。<br />
<br />
由于，这个原本中性之辞，已被极端派『民粹』学者操作，变成有了贬意之词，所以多数日本人现在改称「中国」了(除了骂中国的时候...)。不过在台湾，也有少数仇视中国的人，引用作为，轻蔑自称「纯中国人」之语。<br />
<br />
原本，支那，是由 China音译回来。因中国古代由丝路传到欧洲国家，最多的物品，为丝绸与瓷器。故以瓷器(china:需小写)来代称中国(CHINA：需大写)。而支那人就是指 Chinese (汉人)，是尊敬应是荣衔。</div>

							
							
							
							
