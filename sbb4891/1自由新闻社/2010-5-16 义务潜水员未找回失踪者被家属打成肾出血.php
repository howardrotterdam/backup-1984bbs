<br />
船长赵美桂是温岭石塘本地人，他的尸体没有找到。家属坚持要让郭文标继续潜水搜索，郭文标说已经找遍了也没找着，结果情绪激动的家属开始打郭文标，造成郭肾出血，住院治疗。<br />
