　　当公共汽车越来越接近文戈家布伦斯威克时，年轻人们坐在靠窗户的位上往外看，盼望着布伦斯威克快点到来，只有文戈不敢张望，他正鼓足勇气准备迎接失望。突然间，全车的人都沸腾起来，站起来又喊又叫又跳。文戈却坐在那望着老橡树惊呆了，树上系满了黄手帕，二十条、三十条，或许几百条，这些黄手帕像欢迎的旗帜迎风飘扬，这时文戈站起身来，向家走去。<br />
<br />
这个故事刊出不久，很快就出现了这首不朽的音乐作品。这个动人的故事被作成了歌曲 Tie A Yellow Ribbon Round The Ole Oak Tree（老橡树上的黄丝带），伴着歌声这个故事也传遍了全世界。後来并在美国成为一种风俗:在家门前的树上系上黄丝带,以欢迎久别归来的亲人.</div>

							
							
							
							
