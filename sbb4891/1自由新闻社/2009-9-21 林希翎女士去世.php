																											<div id="postmessage_252152" class="t_msgfont">林希翎：中国１９５７年右派的代表与象征（1）<br />
作者： 钱理群 <br />
<br />
<br />
林希翎是１９５７年右派的代表性、标志性的人物，这不仅是因为她当时影响很大，她在１９５７年的活动及以后的种种遭遇，涉及党的上层、民主党派、文艺界、新闻界与校园里的大学生――这几个方面正是鸣放与反右运动的主战场；而且她是至今未平反的右派，是特意留下来以证明反右运动的正确性与必要性的“标本”，这样，就把她推到了一个“历史人物”的地位：她成了中国１９５７年右派及其精神的一个象征。但她自己却一再声称，这是一个历史的大误会：“我会成为政治人物，新闻焦点，全是历史的错误。我痛恨政治；我只是希望在以后的岁月里，过一个正当人的生活，搞一些文学创作和学术研究”1。这里所包含的不得不扮演历史所派定的角色的个人性的悲剧，同样具有典型性。 <br />
<br />
因此，我们研究“１９５７年学”，不能不谈到林希翎。 <br />
<br />
<br />
（一） <br />
<br />
<br />
我们的讨论，就从中共中国人民大学委员会１９７９年７ 月４ 日拒绝为林希翎平反的所谓《复查结论》说起。该《结论》给林希翎列举了三条罪名：第一条是“１９５７ 年５ 月２３日至６ 月１３日，林借帮助党整风之机，先后去北大和在人大作了六次演讲、答辩，公开煽动要从根本上改变我国社会制度”；第三条是“反对当时中央的整风方针和部署，煽动同事“；最重要的是第二条：“公布、抄写赫鲁晓夫的‘秘密报告’，大反斯大林，制造混乱”，这一条确实是最具林希翎“特色”的：她当时影响大，也就是因为她在第一条所说的北大、人大演讲中，公开提到了赫鲁晓夫“秘密报告”，所谓“煽动改变社会制度”、“反对中央整风方针和部署”，其“依据”都是她由赫鲁晓夫秘密报告引出的对中国现实的议论。《结论》还具体举出了林希翎有关的“罪恶”事实：“整风运动初期，林搞到一份赫鲁晓夫秘密报告。她明知这个‘报告’在国际共产主义运动中已经造成严重恶果，她也知道不得外传，却在演讲、发言中，都大量引证其内容。她攻击说：‘斯大林专横残暴，严重透顶，历史皇朝无可比拟’，‘阻碍社会发展，倒退了一个时代’。她声称‘秘密报告是很真实的’，给我‘很大启示’。她用‘秘密报告’来影射我们党的工作，说‘我们同志间关系不正常，六亲不认，冷若冰霜’。她在六月一日全校大会上宣布：‘现在我主张公开赫鲁晓夫的秘密报告，……我这里有一份，可以公开’。六月二日她贴出海报，要在六月三日晚上公布，由于校领导和广大师生的反对，她未能得逞。但使学校的整风运动被迫停止了三天。林还把‘报告’给校内同学传阅，又叫人抄寄给北大、西安、南京、武汉等地一些人。当学校让她交出时，她又抄留了一份。林希翎的这些言论和活动，在校内外造成恶劣影响，严重地干扰破了学校整风运动，也使外单位、外地的一些青年跟着犯了新的错误”。2尽管这里的价值判断很难让人认同，但其所叙述的事实，却提醒我们注意到，１９５７年的鸣放运动及其后的反右运动的国际背景：由赫鲁晓夫的秘密报告引发的国际共产主义运动内部的变动与论争。在某种意义上可以说，整风与反右都是对赫鲁晓夫的秘密报告的中国反响。而这一反响，又包括两个层面：中国最高领导层的反响与中国知识分子，特别是青年大学生的反响，正是这两者之间的相互纠缠，决定了整风与反右运动的发动，走向与结局。 <br />
<br />
因此，我们的讨论还要追溯到中国最高层对赫鲁晓夫秘密报告的反应。据时为新华社社长的吴冷西回忆，赫鲁晓夫关于斯大林问题的报告是１９５６年２ 月２４ 日晚在苏共２０ 大最后一次秘密会议上作的，中共代表团没有参加这次会议，只是会后由一位联络员作了通报：把报告宣读一遍就拿走了。3“但是，在苏共２０ 大结束不久，西方通讯社就陆续透露这个报告的内容。纽约时报在３ 月１０ 日发表了这个报告的全文，距苏共２０大结束不到半个月。新华社收到纽约时报后马上组织大量人员翻译，译出一部分即印出一部分，全部译完后再装订成本，按照中共中央办公厅开列的名单，分送中央负责同志”。吴冷西清楚地记得，３ 月１７ 日晚，他突然接到通知，到中南海列席中央书记处会议，议论赫鲁晓夫秘密报告。会议参加者有毛泽东、刘少奇、周恩来、朱德、邓小平、彭真等最高领导人，以及杨尚昆（时为中央办公厅主任）、胡乔木（中宣部副部长）、王稼祥（中央联络部部长）、张闻天（外交部常务副部长）。据他回忆，毛泽东当时还没有将报告全文看完，但在会上对报告作了两点评价：“一是他揭了盖子，一是他捅了漏子。说他揭了盖子，就是讲，他的秘密报告表明，苏联、苏、斯大林并不是一切都是正确的，这就破除了迷信。说他捅了漏子，就是讲，他作的这个秘密报告，无论在内容上或方法上，都有严重错误”。在吴冷西的感觉中，毛泽东的两点论尽管没有展开，却是对赫鲁晓夫的秘密报告作了“破题”。4吴冷西的感觉是有道理的：毛泽东以后的一系列思考与行动部署其实都是基于这样两个基本判断。据李慎之回忆，原新华社副社长的陈适五当时就跟他说：“毛主席此刻的心情是一则以喜，一则以忧”5，这也是一个很深刻的观察。所谓“喜”，是因为“揭了盖子”，解除了长期压在毛泽东头上的“紧箍咒”。毛泽东后来在与南斯拉夫共产主义者联盟代表团的谈话中，就说到他一生写过三篇歌颂斯大林的文章，都“不是出于内心的意愿”，因为自己所要反对的“王明路线，实际上就是斯大林路线”，而且斯大林还一直视毛泽东为“半个铁托或准铁托”，“不仅苏联，就是其他社会主义国家，和非社会主义国家中，都有相当一些人曾经怀疑中国是否真正的革命”，这对中国共产党与毛泽东本人的压力是可以想见的。现在，赫鲁晓夫将斯大林的“盖子”揭开了，毛泽东自然有“解放”之感，甚至称之为”一场‘解放战争’，大家都敢讲话了，使人能想问题了”6，最重要的是，从此，毛泽东可以放手按自己的意愿行事，走自己多年梦想的中国，以至世界革命的“圣人”之路了。但赫鲁晓夫全盘否定斯大林，却又使毛泽东觉得这是捅了一个“大漏子”。深知毛泽东的胡乔木曾经说过：“毛主席在很长的时间认为，他就是中国的斯大林”，其所受的“国际范围内的集权制加上党内的集权制”的斯大林模式的影响是非常大的；因此，“二十大批判了斯大林，这对于毛主席的刺激是非常深的”。7而赫鲁晓夫对待斯大林的“方法”，即生前处处顺从，死后用突然袭击的方式发难，全盘否定，大概也给毛泽东以极大刺激，由此形成的所谓“赫鲁晓夫情结”，是预伏着以后的文化大革命的。正因为有了这一“喜”一“忧”，就有了毛泽东的一系列反应。大体说来，从１９５６年４ 月５ 日发表《关于无产阶级专政的历史经验》到１９５６年１２ 月２９ 日《再论无产阶级专政的历史经验》发表，毛泽东反应的重心有一个微妙的重要的变化。据参与起草这两个文件的吴冷西回忆，毛泽东对“一论”作了四处修改，“一是明确指出斯大林的主要错误，并且指出产生这些错误是由于他思想方法上的主观主义和片面性，脱离实际和脱离群众，违背群众路线和集体领导；二是加强了关于社会主义社会仍然存在矛盾一段；三是在有关中国党历史上的路线错误段落，突出了两次王明路线和建国后高饶反党集团；四是强调应以历史的观点看待斯大林”。这四点尽管含有为赫鲁晓夫补“漏子”的意思，但重心还是支持赫“揭盖子”。因此，毛泽东在最后作总结时，一再强调重要的是“我们自己从中得到什么教益”，他认为最主要的是“要独立思考，把马列主义的基本原理同中国革命和建设的具体实际相结合”，并且重申“现在感谢赫鲁晓夫揭了盖子，我们应从各方面考虑如何按照中国的情况办事，不要再像过去那样迷信了。……现在更要努力找到中国建设社会主义的具体道路”。8随后，于４ 月２５ 日作《论十大关系》的报告，４月２８ 日在中央政治局扩大会议上提出“百花齐放，百家争鸣”的方针，这都可以看作是毛泽东和中国共产党人寻找“中国建设社会主义的具体道路”的自觉努力和尝试。9前引与南斯拉夫代表团的谈话，也就是在这样的背景下作出的。但从十月以后，先后发生了波兰事件，匈牙利事件，１１月１１日铁托在普拉（南斯拉夫西部沿海城市）的演说，指出斯大林的错误“是一种制度的产物”，“不仅仅是一个个人崇拜问题，而是一种使得个人崇拜得以产生的制度问题”，引发了共产主义运动内部的激烈的论争； <br />
<br />
而铁托提出要反对“斯大林主义”，这大概更是给自命“斯大林”的毛泽东以极大的刺激。 <br />
<br />
正是在这样的背景下，在毛泽东的主持下，又写了《再论无产阶级专政的历史经验》。据吴冷西回忆，在文章的酝酿、写作与修改过程中，毛泽东把重心转向了“捅了漏子的后果”，即所谓“全世界出现了反苏反共浪潮，帝国主义幸灾乐祸，国际共产主义队伍思想混乱”，并且表示：“我们要硬着头皮顶住，不仅要顶住，而且要反击”。毛泽东还据此作出了两个重要的判断：“当前反苏反共浪潮是国际范围的阶级斗争尖锐化的表现”；“当前的问题是教条主义还没没有肃清，又来了修正主义思潮，而且来势凶猛”，因此，斗争的“主要锋芒是反对修正主义”，要“把斯大林这把刀捡起来，给帝国主义一刀，给修正主义一刀”。毛泽东还指示，《再论》“特别要讲清楚斯大林的错误不是社会主义制度造成”。10于是，就有了这样的论述：“苏联经济迅速发展的事实证明，苏联的经济制度基本上是适合于生产力的发展的，苏联的政治制度也是基本上适合于经济基础的需要的。斯大林的错误并不是由社会主义制度而来；为了纠正这些错误，当然不需要去‘纠正’社会主义制度。文章还针对对国家计划管理的“官僚主义机构”的批评，强调“苏联经济的高涨正是劳动人民的国家政权有计划的管理经济事业的结果，而斯大林所犯的主要错误，却很少同管理经济的国家机关的缺点有关”。文章同时又提出：“在基本制度适合需要的情况下，在生产关系和生产力之间，在上层建筑和经济基础之间，也依然存在着一定的矛盾。这种矛盾表现为经济制度和政治制度的某些环节上的缺陷。这种矛盾，虽然不需要用根本性质的变革来解决，仍然需要及时地加以调整”。这都是回应铁托的，大概就是“给修正主义一刀”吧。《再论》因此发出警告：“那些离开无产阶级专政而高谈民主的‘社会主义者’，实际上是站在资产阶级方面而反对无产阶级，实际上是要求资本主义而反对社会主义”。在分析匈牙利事件发生的原因时，《再论》还特别强调了“过去时期的匈牙利还没有认真地建立起无产阶级专政”，“反革命分子没有受到应有的打击”，11这背后所暗含的意思，人们也许要经过以后一系列的事变才能恍然大悟。 <br />
<br />
因此，《再论》虽然主要讨论国际问题，但其国内的背景却是依稀可见的。同年１１月１５日，毛泽东在八届中央委员会二次会议的讲话中，就提出了“里通外国”的问题，并且说这样的人“高级干部、中级干部”中都有。高饶事件又被重新提起――其实毛泽东在断定“当前反苏反共浪潮是国际范围的阶级斗争尖锐化的表现”时，大概就想起了高饶事件，因为他早已断定，高饶反党联盟的出现，“是我国现阶段激烈阶级斗争的一种尖锐的表现”，在他看来，国际与国内问题总是有内在联系的，他也因此早已发出过要“准备对付（帝国主义发动的）突然事变，准备对付反革命复辟，准备对付高饶事件的重复发生”的警告。12显然在毛泽东的估计中，这三大危险在１９５６年的国际、国内形势中，是更为严重的，必须认真对付。他甚至发出了这样的自我警戒：“像我们这样的人，可能犯错误，结果斗不赢，被别人推下去，让哥穆尔卡上台，把饶漱石抬出来”。13这些话看似随意说出，却是更真实地道出了毛泽东内心的紧张，某种不安与隐忧，以后的种种举措是可以从中找到某种心理动因的。因此，当他听到有“司局长一级的知识分子”提出了“大民主”的概念，就立刻敏感到这是代表了一种“思潮”，14说“他们要搞的‘大民主’，就是采用西方资产阶级国会制度，学西方的‘议会民主’、‘新闻自由’、‘言论自由’那一套”，并且也提出了对付的办法，即用“无产阶级领导下的大民主”去反击“资产阶级大民主”，也就是开展大规模的“轰轰烈烈的群众运动”大搞阶级斗争，“狠狠地斗一下”。15半年以后的反右运动其时已经开始酝酿于心，只不过人们一时未能领悟而已。同时，他也说：“大民主也可以用来对付官僚主义者”，16毛泽东一生对“官僚主义者”都无好感，他们也是他达到自己的目的的阻力。他发动整风运动当然也含有借助群众运动的力量来“整”一下“官僚主义者”的用意。但他区分得很清楚：“官僚主义者”是“人民内部矛盾”：毛泽东心里明白，自己的统治是从根本上离不开这些官僚的；而右派则是“敌我矛盾”，是他必除之而后快的。 <br />
<br />
到１９５７年在省市自治区党委书记会议上的讲话就说得更为清楚。因为据说这时国内已经出现了一些“小匈牙利”事件。毛泽东在讲话中特意提到了石家庄一所学校，有“少数反革命分子”利用学生对毕业分配的不满，煽动游行，要夺权电台，并高喊“打倒法西斯！”“要战争不要和平！”、“社会主义没有优越性！”的口号；毛泽东因此说：“匈牙利事件的一个好处，就是把我们中国的这些蚂蚁引出了洞”。“蚂蚁”存在在哪里呢？毛泽东有个分析：除“地主，富农，还有恶霸和反革命”这些“被剥夺的阶级”之外，“有些民主人士和教授”“跟我们也是对立的”，“学生中跟我们对立的也不少”。对学生的“思想动向”，毛泽东似乎特别注意，并作了这样的估计与分析：“我们高等学校的学生，据北京市的调查，大多数是地主、富农、资产阶级以及富裕中农的子弟，工人阶级、贫下中农出身的还不到百分之二十。全国恐怕也差不多。这种情况应当改变，但是需要时间。在一部分大学生中间，哥穆尔卡很吃得开，铁托、卡德尔也很吃得开”。在毛泽东看来，这些学生不过是“地主，富农，资产阶级，民主党派”的代言人，“他们老于世故，许多人现在隐藏着。他们的子弟，这些学生娃娃们，没有经验，把什么‘要杀几千几万人’、什么‘社会主义没有优越性’这些东西都端出来了”。既然客观存在，但又“隐藏”着，并且迟早要引爆，不如现在就将其引诱出来，“让他们唱对台戏”，“他们有屁就让他们放，放出来有利”，“他们要闹，就让他们闹够。多行不义必自毙”。毛泽东当时把这称作“让它暴露，后发制人”策略，17其实就是后来著名的“引蛇出洞”的“阳谋”。到形势已经明朗，要正式发动反右运动时，毛泽东就有了一个更明确的说明：“这是一场大战（战场既在党内，又在党外），不打胜这一仗，社会主义是建不成的，并且有出‘匈牙利事件’的某些危险。现在我们主动的整风，将可能的‘匈牙利事件’主动引出来，使之分割在各个机关各个学校去演习，去处理，分割为许多小‘匈牙利’，而且党政基本上不溃乱，只溃乱一小部分（这部分溃乱正好，挤出了浓包），利益极大”。18李慎之先生说得很对，这一“引蛇出洞的战略部署起意于八届二中全会而决策于省市委书记会议”。19也就是说，到１９５７年１ 月，罗网已经撒下，只是如何“引蛇”，“蛇”又如何“出洞”的问题了。 <br />
<br />
这就说到了事情的另一面：当毛泽东已经对苏共二十大赫鲁晓夫的秘密报告引发的国际共产主义运动的大变动、大争论，作出自己的国际应对（《二论》、《再论》）与国内部署时，尚不知就里的中国知识分子，特别是大学里的热血青年，却按照自己的理解、理想与意愿作出了独立的，以后却要为之付出巨大代价的反应。 <br />
<br />
正如李慎之先生所说，“关于苏共二十大，赫鲁晓夫的报告既然是秘密报告，中共当然也不会公开宣传，但是事实上中国人知道的范围很广。因为中央决定把朱德从莫斯科带回来的译文印成小三十开的小册子，封面上题目都没有，只印有‘内部材料．注意保存’八个字，随《参考资料》发放。《参考资料》虽然只发到高级干部，但是并不算是机密刊物，也只是刊头上印有 ‘内部刊物，注意保存’两行字。因此这个报告的内容实际上传播极广。另外外文书店还发售美国的《工人日报》，上面也载有英译文的全文，北京各大学的学生竞相购买，竟致把《工人日报》买光。尤其是毛主席当时一再发出要打防疫针，种牛痘的主张，《参考消息》由于他的旨意而由２０００份扩大发行到４０万份，每个大学生都可以订阅。看来他并不反对让这个报告的内容从各个渠道泄露出去。因此，这个报告已算不得什么秘密了。《参考消息》不能登赫鲁晓夫报告的内容，但是可以登各国的反应。”20而在北京大学这样的校园里，自由、多元的传统还在无形地发挥作用，如时为生物系的助教姚仁杰先生所回忆：“那时的学校对图书的管制还没有后来那么厉害和严密”，“好读书而又想追求甚解”的老师和学生，也还能“偷得天火”21。据鸣放时期校园内最活跃的北大百花学社的主要组织者人之一的陈奉孝回忆，当时北大数学系的助教任大熊从北大图书馆借出了刊载有秘密报告的《工人日报》，和陈奉孝、陶懋颀一起将报告翻译了出来，在一部分同学中传阅（后来他们三人都被打成右派，任大熊被判无期徒刑，在劳改对里折磨而死）。另一些同学则通过苏联和东欧的留学生多少了解了报告的内容。22还应该提到的是，新复刊的《文汇报》连载了安娜．路易斯．斯特朗的回忆录，对过去竭力隐蔽的“斯大林时代”的社会生活的负面有真切的揭示，也产生了广泛的影响。她所引述的英国阿克顿勋爵的名言：“权力使人腐化，绝对的权力绝对地使人腐化”，在当时引起了震动，以后更是不断地被人们所引用。根据毛泽东的指示，铁托１９５６年１１月的演说与各国共产党的评论，都曾在中国报刊上公开发表；由于《再论》中谈到南斯拉夫“在企业和其他社会组织中实行民主管理的试验”，中国报刊上也有了一些相应的介绍：这些，都打破了苏联模式的一统天下，引发了许多独立的思考。新的思想解放的运动开始在孕育中。 <br />
<br />
现在我们终于可以回到本文的主要讨论对象林希翎这里来。林希翎曾因在１９５５年在《文艺报》上发表长篇文艺论文而引起广泛关注，后又因为一篇与苏联权威杂志《共产党人》商榷的文章，遭到无端攻击，她因而印发了《一个青年公民的控诉书》，得到时为人民大学校长的吴玉章及任团中央书记的Hu Yaobang的支持而获平反。Hu Yaobang及《中国青年报》的主编遂派她作为特约记者到西北地区作调查。这一次调查，以及后来她在法院实习的所闻所见，都让她看到了中国社会，特别是底层社会的种种弊端，她为之诧异不已：在此之前，她几乎是生活在“云端”，轻信人们告诉自己的一切，长久地沉湎在梦幻中；怀疑就这样产生了。８０ 年代她在接受采访时，这样谈到自己的思想蜕变：“我原来没离开过大城市，一下子到农村，到山区到玉门油矿，到甘肃兰州都跑了一下，就完全感到农村生活，不像《人民日报》吹牛吹的那个样子――电灯，电话，什么农民生活怎么好怎么好；有的苦得要死，也没饭吃，我非常痛苦，我觉得要说。我们那个时候非常理想主义，想为人民服务，为了解放人民，我们才来革命的。结果呢，人民生活这么痛苦，我们在北京里过不错的生活，在这样的一种情况下，我对这些问题，就开始深思：究竟为什么？”23就在这个时候，她从自己的恋人（时为Hu Yaobang的秘书）那里，看到了赫鲁晓夫的秘密报告。鸣放时期，她在人民大学的一次演讲中，这样谈到她当时受到的震惊：“对于斯大林过去我的印象是很好的，因为苏联电影与苏联小说我看得很多。当斯大林逝世时，我在部队里戴上黑纱哭肿了眼，当最初听说赫鲁晓夫在苏共二十大上批判斯大林时，我想不通，也很生气。但当我亲眼看到这个‘秘密报告’时，我才大为震惊，我感到我过去的眼泪都是白留了。还幸亏斯大林死了，要是再多活几年，还不知道要害死多少正直善良的苏联共产党人和公民。我感到对他在军事上、农业上和对外关系方面犯的错误，都还可以原谅，只是在肃反扩大化这个问题上所犯下的严重错误，甚至也可以说对党和人民所犯下的罪行，在感情上是不可原谅的”。24“我感到我过去的眼泪都是白流了”，这上当受骗的屈辱、痛惜、悔恨后的清醒，让人想起了鲁迅《狂人日记》里那句著名的话：“才知道以前的三十多年，全是发昏”。25或许就在“不再相信眼泪”的这一瞬间，林希翎和她的同代人中的一部分，开始与幼稚、单纯而轻信的“童年”时代告别了。斯大林的（还有中国的）肃反运动受害者的血，将所有的迷信，笼罩在苏联、斯大林、领袖、某种绝对理念与体制……头上的神圣光圈，都颠覆，击碎了。如姚仁杰先生在他的回忆录里所说：“不同的思考和看法油然而生，有如翻江倒海，习惯性的信仰和被别人灌输的一致性轰然动摇，连自己对自己也不那么迷信了”。 <br />
<br />
这里，或许可以插叙一段我个人的一些北大校园的亲身体验。我是１９５６年９ 月考入北京大学中文系新闻专业的。在初入学时，还沉浸在响应党的“向科学进军”、“百花齐放，百家争鸣”的号召，发奋读书，准备将来当作家、学者的梦境中，但接着而来的波兰事件、匈牙利事件……，却打破了校园的宁静。我至今还清楚地记得，半夜里，我们这些共青团员突然被从睡梦中叫醒，集合到大礼堂去看一部反映匈牙利事件的内部记录片：那“反革命分子”将共产党员的头挂在电线杆上的镜头，让我们看得胆战心惊。接着报纸上又发表了铁托的讲话，以及相关的争论文章，还有关于南斯拉夫民主试验的介绍……，对于我们，都是闻所未闻，一时，竟有无所适从之感。由此引发了莫名的内心骚乱，隐隐地感到，一个“校园内放不下一张平静的书桌”的时代似乎正在逼近，但刚刚入学的我，似乎还没有尝够泡在图书馆里的安适、闲寂的乐趣，于是，对即将到来的一切，既怀着期待，又感到不安。我的这种心境，或许与前述林希翎式的觉醒意识有所不同，但却也是反映了思想大一统的局面正在发生动摇：连我这样的并不关心政治，一门心思读书的普通学生，也感到了变动的气息。而且不关心政治当时也是不被允许的：《再论无产阶级专政的历史经验》发表后，班级与团支部都组织我们学习。我记得，当时争论最激烈的，就是斯大林的错误的根源何在？与制度有没有关系？在《再论》发表之前，这个问题，同学们私下就议论过。当时的权威观点还是《关于无产阶级专政的历史经验》的说法，即产生错误的原因是斯大林“思想方法上的主观主义和片面性，脱离实际和脱离群众，违背群众路线和集体领导”。在当时的背景下，自然不会有人公开质疑这一党报也即党的观点，但其缺乏足够的说服力是许多人心里都明白的。铁托的演说直接了当地提出了制度的问题，就使怀疑显得似乎更有依据了――由于铁托曾受到斯大林的排挤和迫害，当时许多大学生都对他抱有好感。现在《再论》一方面明确反对铁托的意见，但又承认“经济制度和政治制度的某些环节上”存在“缺陷”：这非但没有解除疑惑，反而更增加了思想的迷乱。在学习时，大家反复讨论如何理解所说的“某些环节”上的“缺陷”：是哪些“环节”？什么是“根本制度”？这两者的关系如何？……等等，都是说不清楚的。记得周扬还专门来作过一个报告，谈及制度问题，大家听了依然觉得一头雾水。</div>

							
							
							
							
