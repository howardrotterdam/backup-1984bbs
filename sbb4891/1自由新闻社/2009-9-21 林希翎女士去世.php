																											<div id="postmessage_253356" class="t_msgfont">林希翎(1935― ），原名程海果，1935年出生，浙江温岭人。1949年考入温岭中学高中部。同年年夏参加了解放军第二十五军，曾任师文工队员。五十年代前期转业到中国人民大学法律系学习。曾被当时担任共青团中央书记的Hu Yaobang同志誉为“最勇敢最有才华的女青年”。可是她却“不守本分”，从当时文坛几个“热点人物”林默涵、李希凡和蓝翎三人姓名中各取一字，合成 “林希翎”三字，为笔名，发表了一系列文学论文，引起争论并受到批评，她不服，进行反驳。<br />
<br />
1957年开始“大鸣大放”时，她在人民大学和北京大学连续发表了几次“惊世骇俗”的演说，从而“一鸣惊人”，一度被誉为“勇敢的化身”、“带刺的玫瑰”。随之而来的反右运动中，她遭到全面批判，成了“学生大右派”、“反党急先锋”和“戴着天使面具的魔鬼”等等。<br />
<br />
由于她“顽固抗拒”，被定成“极右分子”，列为所谓“六大右派”（1）之一，开除学籍，留校监督劳动改造，时身体孱弱，有严重的气喘病，天气一冷便苦不堪言。文革时期又被作为“反革命”逮捕判刑入狱。其本人从此在社会生活和新闻媒介中完全消失。直至1973年经毛泽东主席亲自关心过问才得出狱。<br />
<br />
林希翎为纪念Hu Yaobang逝世20周年撰联<br />
<br />
1984年，当林希翎获准合家定居香港时，西方世界为之一震，海外侨胞也认为这是邓小平领导的党中央的一种崭新的政治风度，一种出于自信的表现。仿佛是一件稀世古董的“出口”，在西方掀起了一股“林希翎热”，法国、美国都争相向她伸出了手，台湾也向她发出了邀请。她与丈夫离了婚，便带着两个孩子到了法国，进了法国社会科学院高等研究院，任法国“中国和平统一促进会”顾问。<br />
<br />
2002年9月26日朱F基总理访法，林以旅法华侨华人代表身份受到接见并在《欧洲时报》发表祝词以贺朱F基总理访法圆满成功。<br />
<br />
2009年3月26日，林希翎于法国巴黎郊区圣卡米拉医院加护病房病榻中，为纪念Hu Yaobang同志逝世二十周年，草撰一联，敬作遥祭，她还提到希望自己最终能够落叶归根。<br />
