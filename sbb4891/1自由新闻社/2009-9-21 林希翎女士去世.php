林希翎：中国1957年右派的代表与象征（二）<br />
 <br />
<br />
<br />
&nbsp; &nbsp; 现在我们终于可以回到本文的主要讨论对象林希翎这里来。<br />
　&nbsp;&nbsp;林希翎曾因在１９５５年在《文艺报》上发表长篇文艺论文而引起广泛关注，后又因为一篇与苏联权威杂志《共产党人》商榷的文章，遭到无端攻击，她因而印发了《一个青年公民的控诉书》，得到时为人民大学校长的吴玉章及任团中央书记的Hu Yaobang的支持而获平反。<br />
&nbsp; &nbsp; Hu Yaobang及《中国青年报》的主编遂派她作为特约记者到西北地区作调查。这一次调查，以及后来她在法院实习的所闻所见，都让她看到了中国社会，特别是底层社会的种种弊端，她为之诧异不已：在此之前，她几乎是生活在“云端”，轻信人们告诉自己的一切，长久地沉湎在梦幻中；怀疑就这样产生了。<br />
&nbsp; &nbsp; ８０年代她在接受采访时，这样谈到自己的思想蜕变：“我原来没离开过大城市，一下子到农村，到山区到玉门油矿，到甘肃兰州都跑了一下，就完全感到农村生活，不像《人民日报》吹牛吹的那个样子―― 电灯，电话，什么农民生活怎么好怎么好；有的苦得要死，也没饭吃，我非常痛苦，我觉得要说。我们那个时候非常理想主义，想为人民服务，为了解放人民，我们才来革命的。结果呢，人民生活这么痛苦，我们在北京里过不错的生活，在这样的一种情况下，我对这些问题，就开始深思：究竟为什么？”[23]<br />
&nbsp; &nbsp;&nbsp;&nbsp;就在这个时候，她从自己的恋人（时为Hu Yaobang的秘书）那里，看到了赫鲁晓夫的秘密报告。鸣放时期，她在人民大学的一次演讲中，这样谈到她当时受到的震惊：“对于斯大林过去我的印象是很好的，因为苏联电影与苏联小说我看得很多。当斯大林逝世时，我在部队里戴上黑纱哭肿了眼.当最初听说赫鲁晓夫在苏共二十大上批判斯大林时，我想不通，也很生气。但当我亲眼看到这个‘秘密报告’时，我才大为震惊，我感到我过去的眼泪都是白留了。还幸亏斯大林死了，要是再多活几年，还不知道要害死多少正直善良的苏联共产党人和公民。我感到对他在军事上、农业上和对外关系方面犯的错误，都还可以原谅，只是在肃fǎn扩大化这个问题上所犯下的严重错误，甚至也可以说对党和人民所犯下的罪行，在感情上是不可原谅的”。[24]<br />
&nbsp; &nbsp; “我感到我过去的眼泪都是白流了”，这上当受骗的屈辱、痛惜、悔恨后的清醒，让人想起了鲁迅《狂人日记》里那句著名的话：“才知道以前的三十多年，全是发昏”。[25]<br />
&nbsp; &nbsp;&nbsp; &nbsp;或许就在“不再相信眼泪”的这一瞬间，林希翎和她的同代人中的一部分，开始与幼稚、单纯而轻信的“童年”时代告别了。斯大林的（还有中国的）肃反运动受害者的血，将所有的迷信，笼罩在苏联、斯大林、领袖、某种绝对理念与体制……头上的神圣光圈，都颠覆，击碎了。如姚仁杰先生在他的回忆录里所说：“不同的思考和看法油然而生，有如翻江倒海，习惯性的信仰和被别人灌输的一致性轰然动摇，连自己对自己也不那么迷信了”。<br />
<br />
&nbsp; &nbsp; 这里，或许可以插叙一段我个人的一些北大校园的亲身体验。<br />
&nbsp; &nbsp; 我是１９５６年９月考入北京大学中文系新闻专业的。在初入学时，还沉浸在响应党的“向科学进军”、“百花齐放，百家争鸣”的号召，发奋读书，准备将来当作家、学者的梦境中，但接着而来的波兰事件、匈牙利事件……，却打破了校园的宁静。<br />
&nbsp; &nbsp; 我至今还清楚地记得，半夜里，我们这些共青团员突然被从睡梦中叫醒，集合到大礼堂去看一部反映匈牙利事件的内部记录片：那“反革命分子”将共产党员的头挂在电线杆上的镜头，让我们看得胆战心惊。接着报纸上又发表了铁托的讲话，以及相关的争论文章，还有关于南斯拉夫民主试验的介绍……，对于我们，都是闻所未闻，一时，竟有无所适从之感。由此引发了莫名的内心骚乱，隐隐地感到，一个“校园内放不下一张平静的书桌”的时代似乎正在逼近。<br />
&nbsp; &nbsp; 但刚刚入学的我，似乎还没有尝够泡在图书馆里的安适、闲寂的乐趣，于是，对即将到来的一切，既怀着期待，又感到不安。我的这种心境，或许与前述林希翎式的觉醒意识有所不同，但却也是反映了思想大一统的局面正在发生动摇：连我这样的并不关心政治，一门心思读书的普通学生，也感到了变动的气息。而且不关心政治当时也是不被允许的――《再论无产阶级专政的历史经验》发表后，班级与团支部都组织我们学习。<br />
&nbsp; &nbsp; 我记得，当时争论最激烈的，就是斯大林的错误的根源何在？与制度有没有关系？在《再论》发表之前，这个问题，同学们私下就议论过。当时的权威观点还是《关于无产阶级专政的历史经验》的说法，即产生错误的原因是斯大林“思想方法上的主观主义和片面性，脱离实际和脱离群众，违背群众路线和集体领导”。<br />
&nbsp; &nbsp; 在当时的背景下，自然不会有人公开质疑这一党报也即党的观点，但其缺乏足够的说服力是许多人心里都明白的。铁托的演说直接了当地提出了制度的问题，就使怀疑显得似乎更有依据了――由于铁托曾受到斯大林的排挤和迫害，当时许多大学生都对他抱有好感。现在《再论》一方面明确反对铁托的意见，但又承认“经济制度和政治制度的某些环节上”存在“缺陷”：这非但没有解除疑惑，反而更增加了思想的迷乱。在学习时，大家反复讨论如何理解所说的“某些环节”上的“缺陷”：是哪些“环节”？什么是“根本制度”？这两者的关系如何？……等等，都是说不清楚的。记得周扬还专门来作过一个报告，谈及制度问题，大家听了依然觉得一头雾水。<br />
&nbsp; &nbsp; 因此，１９５７年５月１９日，北大校园贴出第一批大字报，对于林希翎式的早已破除了迷信，开始独立思考的先觉者，是蓄之已久的必然爆发：这正是他们期待中的继五四新文化运动以后的又一次思想解放，并且他们很快就将其命名为“５・１９民主运动”。<br />
&nbsp; &nbsp; 而对像我这样的普通学生，是既感到突然，又似乎在预料之中的。而且前述“制度”问题又很快成为校园内的争论的焦点之一。各种意见或争锋相对，互不相让，或互为补充、发明，突然展现了一个从未有过的自由思考的思想平台，让人感到振奋；而各级党组织按照毛泽东的指示保持了可疑的沉默，《人民日报》又拒绝报道“北大民主墙”，这就又增添了许多不安。<br />
&nbsp; &nbsp; 林希翎就是在这样的背景与气氛下，于５月２３日登上了北大的讲台。她以其特有的明快，一开口就提出“胡风是不是反革命”这个最敏感的问题，并且立即提及赫鲁晓夫的秘密报告，点明“我国也是肃反扩大化”。<br />
&nbsp; &nbsp; 这样就把赫鲁晓夫秘密报告中所谈到的斯大林的问题与中国的问题联系起来：这正是犯了大忌。于是，她以后的所有论述都被理所当然地认为是针对中国现实的，这就注定了林希翎成为“不可饶恕的右派”的命运。<br />
&nbsp; &nbsp; 她是这样说的：“我有很多问题同意南斯拉夫的看法，铁托演说中很多是好的。我就认为个人崇拜是社会主义制度的产物。马克思主义告诉我们，所有社会现象都有社会历史根源，斯大林问题绝不是斯大林个人的问题，斯大林问题只会发生在苏联这样的国家，因苏联过去是封建的帝国主义国家，中国也是一样，没有资产阶级的民主传统。我觉得公有制比私有制好，但我认为我们现在的社会主义不是真正的社会主义，如果是的话，也是非典型的社会主义。真正的社会主义应该是很民主的，但我们这里是不民主的。我管这个社会叫作封建基础上产生的社会主义，是非典型的社会主义，我们要为一个真正的社会主义而斗争”。<br />
&nbsp; &nbsp; 她还说：“我经过研究，认为历史上所有的统治阶级都有一个共同点，他们的民主都有局限性，在革命大风暴中和人民在一起，当革命胜利了就要镇压人民，采取愚民的政策，这是最笨的办法。……人民群众不是阿斗，真正要解决问题只有靠历史创造者人民群众行动起来”。<br />
<br />
她最后以这样一句话来结束自己的演讲：“我们要建设真正的社会主义，让每一个人过真正的人一样的生活！”[26]<br />
&nbsp; &nbsp;&nbsp; &nbsp;――可以想见，林希翎的这一番爆炸性的讲话，会引起怎样的反应，记录稿中说，当场有人为她鼓掌，要求她签名，也有人轰她，甚至递纸条辱骂她。我当时也在现场，可以证实这都是事实，林希翎的演讲确实把北大内部本来就存在的两大派（反右运动以后就被划分为“左派”与“右派”）的争论推向白热化：我记得听完演讲后，回到寝室，两派同学还在争论不休，有的甚至论战了一个通宵。我自己倒没有卷入争论，是因为当时年纪尚小，林希翎提出的许多问题是原来没有想过的，一时也想不清楚，觉得她的某些言词似乎有些偏激；但同时又确实非常欣赏她的独立思考，敢想敢说，认为无论她的观点正确与否，她所提出的问题是应该认真思考的，而她的发言正是打开了一个新的思路，应该允许与鼓励，而不能压制，对那些轰她的人我是反感的，觉得这有违北大的民主传统。――正是这一态度，使我后来被划为“中右”：这在当时北大学生中也是有一定代表性的吧。<br />
&nbsp; &nbsp; 今天我们再来看林希翎当年的发言，有几点是值得注意的。<br />
&nbsp; &nbsp; 首先，她的演说是立足于一个基本立场的，即是她所说的“真正的社会主义”。这绝不是一种言说的策略、姿态，而是十分严肃的自觉追求。林希翎直到８０年代还坚持这一点：“我是把社会主义作为一种想来对待的”；[27] 另一位５・１９民主运动的代表人物谭天荣当时就明确表示：“社会主义这是我们自己的理想。完全不需要从外面输入进来。可是现在这些现象，我们反对的那些东西，不是社会主义本身，而是对社会主义的歪曲。用哥穆尔卡的话来说，我们要反对的是那种把威信建立在血、牢狱与欺骗的基础之上的本国版的贝利亚主义”。[28]<br />
&nbsp; &nbsp;&nbsp;&nbsp;绝不能将５・１９民主运动视为“fǎn社会主义的运动”，社会主义是那一代人的理想，这一点是不能含糊的：当年对包括林希翎在内的“右派”横加“fǎn社会主义”的罪名，是将斯大林式的、毛泽东式社会主义模式视为正宗；今天我们也不能因其对斯大林式、毛泽东式的社会主义的批判立场，而将其看作是“fǎn社会主义”。<br />
&nbsp; &nbsp; 科学的态度应该尊重林希翎们对“社会主义”的独立理解与阐释：从林希翎的演说中，可以看出，她所追求的“真正的社会主义”包含两个基本内容，即“社会主义的公有制”与“社会主义民主制”。这几乎是当时绝大多数有自觉追求的右派的共同立场。因呼吁为胡风平反而很有影响的刘奇弟，在一篇阐述他对北大民主运动基本精神的理解的大字报里，以“社会主义公有制万岁”与“社会主义民主万岁”作结束，[29]这恐怕不是偶然的。这样一种社会主义观，是带有浓重的“社会民主主义”的色彩的；而在毛泽东看来，这正是典型的“修正主义”。<br />
&nbsp; &nbsp; 有意思的是，林希翎们用他们的“真正的社会主义”观看斯大林式、毛泽东式社会主义，却发现了“封建主义”，她在演说中，将其称为“封建基础上产生的社会主义，是非典型的社会主义”。她后来在北大的第二次演讲及《我的思考》等文章中，有更具体的说明：在她看来，当时中国社会所存在的庞大的官僚机构，特权，等级制度，对思想、言论自由的压制，对公民权利的剥夺，愚民政策，宗派主义，某些领导的“地主恶霸的作风”……都带有浓重的封建性，追其原因则是因为苏联与中国都是由封建社会直接进入社会主义，“没有资产阶级的民主传统”。<br />
&nbsp; &nbsp; ８０年代，林希翎在追述自己当年的思想时说：“在１９５７年的演讲中我还讲过，为了和世界上的资本主义国家相区别，对外来说我们自称社会主义国家是可以的，但是从严格的科学意义上来说，不仅在中国根本未曾建成真正的社会主义社会，就连苏联也未建成真正的社会主义社会。我们和苏联的社会制度的性质，还处在向社会主义社会过渡中的过渡时期”。[30]这一概括大体是符合她当时的思想实际的。<br />
&nbsp; &nbsp; 在林希翎１９５７年的演讲中，还表现出强烈的人民主权意识，即她一再强调的人民是“历史的创造者”，要“人民作主”，“要把真实情况告诉人民，要彻底改革，要发动人民来讨论，社会主义是人民的，不是党员的，应该让全民尽情地提意见”。[31]&nbsp;&nbsp;在她看来，对现有社会主义模式提出质疑，探讨什么是“真正的社会主义”，都是在履行人民主权，发动民主运动的目的也是在于动员人民的力量来推动建设“真正的社会主义”的实验与彻底改革，而不是局限于少数人的，上层的，按照既定的旨意进行的“改良”。[32]<br />
&nbsp; &nbsp;&nbsp; &nbsp;应该说，林希翎演说中的以上几个基本点，也是代表了许多校园内的右派学生的基本立场的。甚至可以说北大校园关于“社会主义制度”问题所展开的讨论，是围绕她所提出的几个问题展开的。年轻的中国社会主义者、共产主义者[33]所关注的正是：什么是我们理想的社会主义？中国的社会主义现实在实现“社会主义民主”与“社会主义公有制”方面出了哪些问题，又预伏着怎样的危险？<br />
&nbsp; &nbsp; 关于社会主义民主，这是１９５７年中国校园内最大的热点话题。<br />
<br />
&nbsp; &nbsp; 百花学社的骨干、哲学系学生叶于生当时写有《我看民主》的大字报，其观点是有代表性的：“民主是先进的社会理想”，“民主化是社会发展的必然趋势”，“在现阶段民主既是手段又是目的”，“社会主义公有制，按其本性说，要求它的上层建筑具有高度的民主性”，“人们在生产资料占有上的平权地位，是全民性的、新型民主的物质基础，人们第一次有可能获得发展个性，自由竞赛的平等机会”，“社会主义民主是最高类型的民主”，这是社会主义的本质决定的。[34]<br />
&nbsp; &nbsp;&nbsp;&nbsp;而另一位哲学系学生龙英华，则在他的引起轰动的大字报《世界向何处去、中国向何处去、北大向何处去》里，明确提出一个纲领性的口号：“我们有了一个社会主义工业化，还应有一个社会主义民主化”。[35] 而且论者强调，“５・１９民主运动”就是实现这一目标的具有重大意义的一次试验，目的是要“形成和发展这样一种民主，不是硬搬苏联的形式，更不是贩卖西欧的形式，而是在今天的中国的社会主义土壤中土生土长的民主制度”。[36]<br />
&nbsp; &nbsp;&nbsp; &nbsp;而最让这些年轻的理想主义者深感困惑与不安的恰恰是，按其理想与本性应是民主化最有力的推动者的社会主义国家，却在民主化问题上表现出一种“保守倾向”，使社会主义民主的“巨大潜能，迄今远没有充分展开”，甚至出现了剥夺人民民主权利的现象。[37]<br />
&nbsp; &nbsp;&nbsp; &nbsp;在他们看来，１９５７年的中国对社会主义民主制度的破坏与危险，主要表现在两个方面。一是“法制不健全、不严肃，民主权利没有严格可靠的保证”，这正是“官僚主义、主观主义、宗派主义”这“三害”的“温床”；[38] 二是“权力的高度集中”，即“共产党对国家政权的绝对控制”与“对国家一切生活的绝对控制”，以及“国家高度权力的集中”。<br />
&nbsp; &nbsp; 批评者尖锐地指出：“任何时代，权力的高度集中，不论是集于个人，还是自称为一贯光荣、正确、伟大的集团，都是极大的危险，而当人民群众被麻痹、被愚昧，就更加百倍的危险！因为如果这个集团犯有严重错误或变质，就没有任何力量足以克服它”。[39]<br />
&nbsp; &nbsp;&nbsp;&nbsp;人们还这样提出问题：社会发展与国家经济发展的权力完全集中于国家，这样的政治经济体制“是否需要改变？”“将国家所有制形式变为归生产者大家庭的直接民主管理的所有制”o“自下而上的建立起人民群众的各种组织直接影响中央政权，并达到使‘社会主义社会制度在它的发展中依靠社会主义的劳动人民的自由行动，而不是依靠国家的力量’”，这样的体制改革，“是否值得考虑？”[40]<br />
&nbsp; &nbsp;&nbsp; &nbsp;由此而提出的是对民主的如下理解：“民主权利除了它的阶级性以外，还有着全民性，即全体未剥夺公民权的人民对政府之约束，后者作为一种暴力机构，很容易伤害人民，人民必须用一种全民平等享有的民主权利来保护自己，来抵制政府可能采取的暴政”。[41]<br />
<br />
　&nbsp; &nbsp;――所有这些问题与思考，都是以民主讨论的形式提出的；因此，讨论中，有人特意重申了“资本主义上升时期”的“一句话”：“我完全不同意你的意见，但我用生命来保证你说这些话的权利”。[42] 这里表现出来的，是那一代的年轻人，对民主的渴望，以及对民主理念、精神、制度的探索精神，这是我们时隔四十多年仍能感受到并深受感动的。<br />
&nbsp; &nbsp; 关于“社会主义公有制”的讨论，则集中在“特权阶级”是否有可能从社会主义体制内部产生，并如何防范这一问题上。<br />
&nbsp; &nbsp; 问题是这样提出来的：物理系学生沈迪克化名“谈谈”，写了一篇题为《谈谈无阶级社会中人的等级》的大字报，从留学生的选派、生产实习、毕业分配……等“日常生活”中的种种不平等现象，提出“在新的时代”是否出现了“高贵等级”、“贱民阶级”这样的新的等级区分的疑问[43]；这个问题十分敏感，自然引起了轰动与激烈争论。<br />
&nbsp; &nbsp; 后来又有一位名叫周大觉的理科学生以“谈论”的笔名写了一篇《论“阶级”的发展》的文章，进一步提出了“随着旧阶级的消灭，新的阶级又起来了”这样的论断，并从生产资料的占有关系、分配、社会地位等方面加以论证，还特别指出“官官相护”的现象表明“已开始自觉地形成一个社会集团，他们互相支持、包庇，有共同的经济、政治、社会地位等特殊的利益”。[44]<br />
&nbsp; &nbsp;&nbsp; &nbsp;后来，周大觉又写了一篇《再论“阶级”的发展》，特意谈到他与一位历史系三年级的同学黄良元的争论。周这样介绍他的论争对手：“中共党员，有一定教条主义习气，诚朴，会思考一些问题的好同志”。据说，经过争辩，双方一致认为，“公有制比私有制优越得多。但我们最感兴趣的问题是如何保证公有制名副其实，就是公有了以后如何正确地分配。我们反对绝对平均主义，但目前的问题……是不合理的悬殊的差别。这样，纵然占有公有，但实际上在分配过程中，无形中一部分占有了另一部分人的劳动”。由此得出的结论是：不能“认为社会发展到公有制就万事大吉”，还必须“考虑如何更完满，以及可能产生的新的阶级分野”，“考虑如何防止党、政府工作人员不脱离群众”，并“要有一定的制度来保证”。另一方面，他们也一致认为，这种政治、经济、地位上的悬殊、不平等还处在发展中，矛盾“现在还不甚尖锐”，同时“制止它发展的因素也在增长，有自觉地自上而下的解决的可能性”。黄良元因此认为应称为“人民内部矛盾”，而不必提到“新阶级”的高度。周大觉则认为，“如果管理、分配、社会地位等问题不得到更好的完善，矛盾可以向前发展，而且基本上（已经）满足‘阶级’关系的定义”。[45]<br />
&nbsp; &nbsp;&nbsp; &nbsp;当时，驳难、响应者都不少；但由于很快转入反右，这类现实尖锐问题的探讨，自然就被戴上“fǎn党、fǎn社会主义”的帽子而被扼杀，历史也就错过了一个机会。<br />
&nbsp; &nbsp; 今天，我们已经看到了以后的历史发展的结果，回过头来看当年的这些充满愤激、陕堑姆⒀裕不能不有一种沉重之感：因为，当这些敏感的热血青年晒擅瘢慷慨陈辞时，他们并不知道我们在前文已经说到的事实：毛泽东早在半年以前，就断定这些大学生“大多数是剥削阶级出生的”，“跟我们对立的人也不少”，而且他们背后站着的是那些随时准备复辟的“隐藏”的阶级敌人，“这些学生娃娃没有经验，把什么……都端出来了”。[46]<br />
&nbsp; &nbsp;&nbsp; &nbsp;这样，当林希翎说到“不要以为共产党用整风的方法，采取改良主义的办法，向人民让点步就够了”，“匈牙利人民的血没有白流！我们今天争到的这一点小小的民主，是和他们分不开的”，[47]<br />
&nbsp; &nbsp;&nbsp; &nbsp;当有的右派在大字报中表明这样的立场和态度：“对一个人或一个政党，抱着毫不保留的态度而跟着走是很错误的，只要它今天一旦不再能代表人民，违fǎn人民（的意志）了，就应该毫不留情地抛弃掉”[48]。<br />
&nbsp; &nbsp; 对此，当权者自然要视为“大敌当前”，对毛泽东则意味着，他要“引”的“蛇”与“蚂蚁”终于“出洞”了。更为致命的，或许是右派的这些激进的观点和立场，也是１９５７年的大多数大学生所不能接受或持有保留的。<br />
&nbsp; &nbsp; 这里，不仅有当时大多数大学生还没有从对党和毛泽东的迷信中解脱出来这样一个基本因素，也还因为右派们所敏感到的中国社会矛盾，当时还处在一个萌芽阶段，整个共和国还在向上发展，许多问题更是生活与思想都十分单纯的一般大学生所难以体会的――其实，我自己当时就处在这样的一个状态；因此，我可以赞赏右派们的探索精神，但却无法认同他们的观点，最多只是承认他们的尖锐批判是一种“预警”，不加注意，发展下去“可能”有这样的危险，却不是现实的危机。<br />
&nbsp; &nbsp; 在这种情况下，林希翎们号召要进行彻底的改革，就很难得到大多数大学生的响应，他们那些过于激烈的言词甚至会引起fǎn感。这都是后来的反右运动能够得到响应的内在原因。鲁迅说过，许多问题，敏感的文学家、知识分子“早感到了，社会还没有感到”，这样的“思想的感觉”的“相差”常常要“到三四十年”。[49]<br />
&nbsp; &nbsp;&nbsp; &nbsp;这几乎是一切先觉者的宿命。<br />
&nbsp; &nbsp; 可悲的是，右派大学生们非但没有听到前引毛泽东在１９５６年年底和１９５７年初所发出的那些警告，还听到了许多鼓励民主的“激烈”言论。<br />
&nbsp; &nbsp; 李慎之先生在他的回忆中，曾记录了毛泽东当时在内部讲话中的若干惊人之论，如“中央成立一个体制小组，专门研究如何划分权力”，“美国发展快，其政治制度必有可学之处”，“苏联只有一个党，到底是一个党好，还是几个党好，看来还是几个党好”等等，[50]<br />
&nbsp; &nbsp;&nbsp; &nbsp;这都可以说是典型的“右派言论”，可能是毛泽东的真实想法，也可能只是一种策略，但类似的言论通过不同途径传到林希翎们的耳里，就只能起到鼓励，甚至是煽风点火的作用。至少是产生了两个错觉。一是以为毛泽东发动整风运动，就是给他们这些“新社会主义的主人”[51] 以一个和毛泽东一起探讨“中国式的社会主义”和发动民主运动的权利。<br />
&nbsp; &nbsp;&nbsp;&nbsp;――其实，不仅是这些涉世不深的大学生，就是李慎之这样的有经验的老革命也产生了类似的错觉：他听张闻天谈到“毛主席说马克思主义大家都可以发展，不要只靠他一个人发展”，就真的“狂妄地”以为自己也可以为发展马克思主义作贡献了[52]。<br />
&nbsp; &nbsp; 这是真正地不懂中国政治的游戏规则：“发展”的权力，甚至“批判”的权力，都只属于领袖，即使是高级干部，更不用说是知识分子，大学生，也只有鲁迅所说的“同意与解释”、“宣传与做戏”的义务，[53]若要乱说，就有“僭越”之罪。同样的话，领袖说了，是伟大的“发展”，小民们说了，就是“fǎn党fǎn社会主义”。<br />
&nbsp; &nbsp; １９５７年的真诚而天真的林希翎们不懂、甚至要硬闯这条中国的政治游戏规则，就遭到了灭顶之灾。<br />
&nbsp; &nbsp; 而且林希翎他们还引毛泽东为同道。<br />
&nbsp; &nbsp; 仔细阅读１９５７年校园里的右派的言论，就会发现几乎所有的人，都对毛泽东抱有好感，甚至崇拜。即使是锋芒毕露如林希翎者，谈到毛泽东，虽也有批评之意，如说毛泽东在胡风问题上“犯了一点小小的错误”，但仍然把毛泽东与斯大林严格区别开来，强调“毛主席可贵的一点在于他有辩证的思想，善于发现错误，改正错误，总结经验，吸取教训”o“个人崇拜中国也有，但毛主席很清醒”。[54]<br />
&nbsp; &nbsp;&nbsp;&nbsp;龙英华在提出“现在是走谁的路，是斯大林路线和南斯拉夫的路线谁胜利的问题”以后，甚至作了这样的判断：“铁托、陶里亚蒂、毛泽东、赫鲁晓夫是现阶段的马克思主义者的代表”。[55]<br />
&nbsp; &nbsp;&nbsp;&nbsp;而且这似乎是当时许多右派学生的共识。谭天荣也是把“五・一九”运动看作是“全国范围的整风――民主运动在北大的表现”，“而中国的整风――民主运动是苏共二十大以来国际反教条主义运动的反映”；他说：“（我）不怀疑毛主席永远支持我们”，并且说：“我们有责任大力支持自上而下的整风运动，看来我们亲爱的毛泽东同志处于十分困难的地位，我们有责任把这次自下而上的民主运动领导起来，把它引向破坏性最小的道路”。[56]<br />
&nbsp; &nbsp;&nbsp; &nbsp;在他和他的同志的理解里，毛泽东发动的“自上而下”的整风运动与北大学生所发动的“自下而上”的民主运动是相互配合的，并且是“国际反教条主义运动”的有机组成部分；毛泽东本人与年轻的大学生们是心心相印的相互支持的战友。<br />
&nbsp; &nbsp; 这真是一个可悲的历史大误会：这些一相情愿的热血青年哪里知道，毛泽东早已认定，“南斯拉夫的路线”是一个“修正主义路线”，铁托、陶里亚蒂、赫鲁晓夫都是修正主义分子，而他本人正准备举起斯大林这把“刀子”，砍向国际与国内的打着反教条主义旗号的右派，以证明自己绝不是“中国的铁托”：这正是他发动“整风运动”的目的所在。<br />
&nbsp; &nbsp; 不懂得毛式政治的中国年青人终于为自己的轻信与天真而付出血的代价：这正是中华人民共和国历史上最沉重的一页。</div>

							
							
							
							
