																											<div id="postmessage_252155" class="t_msgfont">钱理群 林希翎：中国1957年右派的代表与象征（三）<br />
 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;（二）<br />
　 １９５７中国校园发生的这一切，一直处在中国最高领导层的密切注视中。林希翎在北大、人大发表演讲以后，《人民日报》立刻被以“内参”的形式上报，刘少奇遂即作出批示：“极右分子。请公安部门注意”。[57] 可以说，从一开始，“无产阶级专政”之剑已悬在林希翎的头上。<br />
&nbsp; &nbsp; 其实早在１９５６年年底，林希翎就曾上访中南海，却因此引发了中南海内部一场不大不小的风波。当时接待她的是中共中央办公厅秘书室的代表，其中一位王文，是当年的地下党员，解放初期曾担任过叶剑英的秘书，时为秘书室的负责人，就因为前后三次接待林希翎，并将她的意见整理上报，而在以后的反右运动中，却被他的部下、后来在文革时红极一时的戚本禹陷害而打成右派，并株连到家庭：妻子和一个孩子因不堪忍受屈辱而自杀，可以说是家破人亡。连时为中央办公厅主任的杨尚昆，也因为在反映林希翎意见的材料上签了一个“阅”字，在反右运动中被迫作检讨。<br />
&nbsp; &nbsp; 杨尚昆见戚本禹野心太大，想操纵运动，就想整他，戚便求助于毛泽东的秘书田家英，田告之于江青，江报告毛，毛泽东于是亲自召集会议说，你们支持左派，还是右派，插红旗还是黑旗？众皆禁若寒蝉，仅一来自延安的女处长发言说汇报情况与事实不符，在文革期间，这位老干部竟被报复，脑部打成重伤。[58]<br />
&nbsp; &nbsp;&nbsp; &nbsp;但中共党内高层中对林希翎表示赞赏的却大有人在。<br />
&nbsp; &nbsp; 如前所说，时为团中央第一书记的Hu Yaobang在１９５６年底就过问过林希翎被无端攻击一事；据Hu Yaobang１９７９年在宣传部长会议上讲话中回忆，１９５７年他曾和林希翎长谈四小时，在林被捕时，他也曾表示过反对意见。中共元老、时为人民大学校长的吴玉章，更是对林希翎的才华与独立思考极为赏识，在她被打成右派以后，还抱病与之长谈，并明确表示不同意对林的处分，以至在开学典礼的报告中提到校内右派名字时，有意不提林希翎，引起轩然大波。<br />
&nbsp; &nbsp; 还有一位元老、时为内务部部长，后来成为最高人民法院院长的谢觉哉，也曾在家中多次接见林希翎，和她作广泛交谈。但却无力阻止逮捕林希翎，只能借视察监狱之名，看她一眼。[59]<br />
&nbsp; &nbsp;&nbsp; &nbsp;在反右运动中，这些关心过林希翎的中共中央委员们也难逃惩罚：由于他们在党内的地位与影响，当时尚不敢直接加以迫害，就将他们的部下与亲人作替罪羊。Hu Yaobang的秘书曹志雄因与林希翎有恋爱关系，并犯有将赫鲁晓夫秘密报告泄露给她的“罪行”，被打成右派自不待说；吴玉章的外孙兰其邦与谢觉哉的秘书吉士林，仅因为奉吴、谢之命给林希翎写过信或带过口信，也被打成右派，并同样株连亲人：吉士林被赶回老家当农民，老母上吊自杀，妻子被迫离婚，带走了孩子：又是一个家破人亡！[60]<br />
&nbsp; &nbsp;&nbsp; &nbsp;林希翎案还株连到民主党派与文艺界、新闻界的许多人。１９５８年８月将其定为“反革命分子”的《北京市中级人民法院刑事判决书》里，曾列有“被告……与谭惕吾、黄绍f等右派分子相互勾结，继续进行反革命活动”的“罪名”。<br />
&nbsp; &nbsp; 谭惕吾和黄绍f都是当时的民革中央委员和常委，是１９５７年的著名的大右派。谭惕吾是法律专家，她在中共中央召开的座谈会上曾大声疾呼：“中国共产党必须遵守宪法”，主张应该建立制度使人民监督共产党；黄绍f则批评“成绩是主要的，偏差错误是个别的”这样的“公式”，强调不能用强调成绩来掩盖错误，百分之二三的错误案件，在全国范围内不知要造成多数人家破人亡。[61] 这在当时都是属于“恶毒攻击”的言论，就成了反右运动的主要靶子。<br />
&nbsp; &nbsp; 但林希翎却对他们的历史与现实态度几乎是一无所知，据１９８０年她写给邓小平的万言书里所说，她仅仅在北京东四检察院实习时，为办一个案件，与作为全国人民代表来这里视察这个案件的谭惕吾打过交道，并无其它任何联系。但为了说明“校园内的右派与民主党派中的右派是相互勾结、上下呼应的”，就将她们硬拉在一起了：按当时的“革命逻辑”，只要是出于斗争的需要，是否有事实根据是并不重要的。<br />
&nbsp; &nbsp; 林希翎还谈到，到１９７９年谭惕吾的右派被“改正”，又被任命为全国人大法制委员会委员，但因为林希翎仍是右派，在她的所谓“改正结论”里，就仍然留了一个尾巴，谭惕吾始终未签字。[62]<br />
&nbsp; &nbsp;&nbsp; &nbsp;１９５８年林希翎《判决书》上还有一条：“被告与新社会上的部分反动分子，尤其是文艺界的一些反动分子――洪禹平等建立密切联系，相互勾结，对党和国家的领袖及我党的文艺方针等进行了恶毒的诬蔑”。[63] 这又是一个冤案：洪禹平是北京市幻灯制片厂编辑部主任，因同乡关系与林希翎相识不到半年，整风期间他已调往浙江，对林希翎在北大、人大讲了什么，他根本不知道，却也成了林案的“要犯”，连同他哥哥、姐姐一家人都打成右派。[64] 但林希翎与文艺界和新闻界的一些知名人士倒确有联系。据林希翎回忆，当时文坛的最高权威郭沫若读了她所写的《一个青年公民的控诉书》，曾当面表示赞扬与支持，并称其为“才女”。时为《人民日报》主编的邓拓，《中国青年报》的主编张藜群都对她十分重视，据说邓拓还向她谈到肃反运动中的一些内部情况。后来在文革初期，邓拓成了“三家村”的头目，戚本禹给他罗列的一条罪状就是“极右分子林希翎最亲密的朋友”，这又反过来株连到林希翎：不仅将在狱中的她戴上脚镣手铐，关入紧闭室、黑牢达半年之久，而且把她的老母亲抓来开万人大会批斗毒打。[65]<br />
&nbsp; &nbsp;&nbsp;&nbsp;更令林希翎感到痛苦，也让我们大为震惊的是，无数普通百姓也被株连。<br />
&nbsp; &nbsp; 林希翎在《给邓小平的万言书》里含泪写道：“单单北京因同我的关系被打成‘右派’的就有一百七十名，而在全国各地则是不计其数。在我这批株连者中既有我相识的，直接接触过的首长、同志、战友、作家、老师、同学和朋友，甚至还有大学里的工友，更多的则是我的根本不相识、从未见过面的北京和全国各地的支持者和同情者”；“在这些株连者面前，我常常感到自己是有罪的，非常内疚和痛苦。尤其因为我在反右运动初期犯过类似小说《牛虻》中阿瑟的错误。[66]当校党委审查我和社会上与校内外友人的关系时，我是坦然地向组织上交出了我所保存的一切文稿、日记和信件，因为当时我确信我自己以及我的朋友们都是没有任何政治问题的，我同他们的友谊完全是光明磊落的，我是没有任何不可告人的秘密。我完全信任党会查清我和我的同志的问题的。哪里知道正是由于我的这些天真幼稚和对党的愚忠迷信，使我自己挨整受骗都是微不足道的小事，最最痛心的是因此而牵连了一大批无辜者，凡是从我这里交出来的信件的写信者，在反右运动中几乎很难幸免不当右派的（而其中还有许多来信都是从一些读过我的文章给报刊编辑部转来的读者来信，和听过我的演讲的听众来信，几乎都是素不相识的）。即使有个别的幸运儿在反右运动中得以‘瞒天过海’，‘蒙混过关’的话，那么在以后的政治运动中仍是混不过去，还是当了‘漏网右派’，还加上其它帽子”。[67]<br />
&nbsp; &nbsp;&nbsp; &nbsp;王文在他的《为林希翎冤案呼吁》一文里，曾举了一个例子：她的一个同班同学魏式昭（也是志愿军转业的），仅仅因为支部派她帮助和照顾林的生活，就被说成了林希翎的“保姆”，不仅她自己被错划为右派，连她的丈夫和远在四川工作的弟弟，以及他丈夫的十几位部队战友，也统统被错划为右派。[68]<br />
&nbsp; &nbsp;&nbsp; &nbsp;这样的株连法是可怕的：一切与林希翎有接触者，更不用说同情者，都是可疑的敌人，都要收入专政的罗网中。林希翎还谈到，１９６５年她患重病在北京市监狱住院期间，一位叫张凤云的小护士，出于同情，冒险为她发过一封请郭沫若转给毛泽东的信和家信，不料当年将林希翎称为“才女”的郭沫若，竟把信转退到北京市公安局，这位小护士立即被关押起来，当时她已经是两个幼子的母亲，一年后宣布开除团籍和公职，并不给任何生活出路。十多年到处上访喊冤，毫无用处，文革结束后，北京市劳改局仍坚持“不予平fǎn”，理由是林希翎仍是右派，其同情者自然“有问题”。[69]<br />
&nbsp; &nbsp;&nbsp;&nbsp;更有甚者，前引中共中国人民大学委员会对林希翎“不予平反”的《结论》，仍然把“林希翎的这些言论和行动，在校内外造成恶劣影响，……使外单位、外地的一些青年跟着犯了错误”列为她的“罪名”，作为不予改正的理由：受牵连的“外单位、外地的青年”依然有“错误”，其罪魁祸首仍是林希翎，这就意味着，“株连”有理，甚至有功。这样的革命逻辑是令人恐怖的。<br />
&nbsp; &nbsp; 最“致命”的，是株连家人与子女：丈夫无端地受排挤，精神大受刺激，孩子也受“左派”邻居的孩子的打骂，回家问母亲：“妈妈，为什么人家叫你‘大右派’、‘坏人’，又叫我‘小右派’？‘右派’是什么东西？”<br />
&nbsp; &nbsp; 林希翎说：“我这个在各种批斗和毒刑前从未流过泪的战士，在听到我的爱子向我提出这种问题时，忍不住抱着他放声痛哭了”，“虽然我在政治上从来不吃后悔药，对我所走过的道路无论是正确的还是错误的，我都从来不感到遗憾和悔恨，然而我唯一感到万分悔恨的憾事（也是不可饶恕的过错），便是我悔不该结婚和生育啊！像我这样当过‘大右派’、‘反革命’的人，乃是这个社会中政治上的‘麻风病人’，不可接触的‘贱民’，还有什么资格成家立业？有什么权利当贤妻良母？！这真是作孽啊！”[70]<br />
&nbsp; &nbsp;&nbsp;&nbsp;问题是，这样的株连，是制服林希翎这样的不屈的fǎn抗者，维护“专政”的有效性的必要手段[71]：这不仅是体现了一种伦理的残酷性，更是体现了体制的残酷性。这里，还要顺便提及一个事实：据林希翎回忆：“我在大陆犯了反革命罪名坐牢，我的父亲也在台湾被国民党以‘通匪’罪名，判刑坐牢，理由是我父亲与我通信”。[72]<br />
&nbsp; &nbsp;&nbsp; &nbsp;林希翎的特殊性在于，她的命运总是与中国高层政治紧密联系在一起。这里，就说到了对林希翎的“处理”。<br />
&nbsp; &nbsp; 据林希翎在《给邓小平的万言书》里所说，他与北大谭天荣的处分决定是经毛泽东批准的：“开除学籍，留校监督劳动，当反面教员”。[73]<br />
&nbsp; &nbsp;&nbsp;&nbsp;毛泽东在１９５７年７月所写的《１９５７年夏季的形势》一文中，也有过这样的明确指示：“右派学生首领应予彻底批判，但一般宜留在原地管教，并当‘教员’”。[74]<br />
&nbsp; &nbsp;&nbsp; &nbsp;也许就因为有了毛泽东这一指示，周恩来主持召开１９５７届大专院校毕业生大会时，还特地让林希翎参加，在讲话中，也只说她是整风中“犯了错误”的青年。但据说北京市公安局于１９５７年１１月已经整理好逮捕她的材料送北京市委审批，却未立刻批准。<br />
&nbsp; &nbsp; 据王文在《为林希翎冤案呼吁》一文中透露，１９５８年在中山公园开的一次联欢会上，刘少奇问起林的情况，人大学生反映她不承认自己是“fǎn党fǎn社会主义”，没有“低头认罪”。刘少奇说：“那你们应当对她加强监督嘛”。不久，公安部长罗瑞卿就亲临人大，在党委召开秘密会议，宣称“林希翎这样的大右派，在人大是改造不好的，还是交给我吧，我有办法对她进行强制改造”。<br />
&nbsp; &nbsp; 于是在１９５８年７月２１日半夜将林希翎秘密绑架，投入监狱。开始还制造了一个纯属捏造的所谓“殴打监督她的学生宋津生”的口实，宣布对她只是拘留五天，又以“态度不好”改为十五天，最后以“反革命罪”宣布逮捕，随后判处十五年徒刑。[75] 至于公安部长罗瑞卿所说的“办法”，一位半夜审讯林希翎的老情报人员因林表现不驯，在盛怒之下一语道破“天机”：“你看着罢！共产党还对付不了你这个老毛丫头！我要让你年青青地进我这监狱，而把你关到白发苍苍，我要关你一辈子，我要让你断子绝孙！”[76]<br />
&nbsp; &nbsp;&nbsp; &nbsp;但连林希翎也准备“将牢底坐穿”时，毛泽东突然在１９７３年向时为北京市委书记的吴德问起林“在哪里工作，好不好”，于是，林希翎又莫名其妙地被宣布“提前释放”了。[77]<br />
&nbsp; &nbsp;&nbsp;&nbsp;但林希翎也依然逃不脱罗网，特别是１９７５年她到北京上访，又获得了一个“大右派找邓小平翻案”的新罪名，被押回浙江，遭到了更残酷的批斗。<br />
&nbsp; &nbsp;&nbsp;&nbsp;当文革结束，几乎所有的右派都得到“改正”时，林希翎的平反却阻力重重，原因是她无意中又卷入了中共的上层政治斗争。一贯关心林希翎的Hu Yaobang先后作了三次批示，明确表示：“改正有利”，并在林希翎的来信上写了一段鼓励的话：“向你致意，愉快地和过去告别，勇敢地创造新生活”。这里所表现出来的人情味却不符合中国的政治规则，起到了意想不到的效果：这一点上，Hu Yaobang与林希翎一样天真。<br />
&nbsp; &nbsp; 正如一位人大副校长对林希翎所说：“他的批示对你有害”，“你要不找Hu Yaobang，问题倒可以解决，你找了他就麻烦了”。当时，党的高层中所谓“改革派”与“凡是派”的斗争正处于白热化状态，Hu Yaobang是一个焦点人物；在这样的背景下，林希翎的案子变成了一个敏感问题，蜚短流长，不径而走。[78]<br />
&nbsp; &nbsp;&nbsp; &nbsp;Hu Yaobang终于不能再过问林希翎的案件，再加上邓小平仍然坚持反右的“正确性”与“必要性”，这就必然要留下“样板”：于是，中国的政治斗争的“需要”，再一次选中了林希翎，让她充当右派典型。</div>

							
							
							
							
