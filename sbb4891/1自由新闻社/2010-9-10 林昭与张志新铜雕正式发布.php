祭园守园人<br />
神的寂灭，人的永恒――<br />
<br />
今天――2010年九月九日九时九分，京北回龙观铁玫瑰园，一对缀满白花的花篮，被敬奉在终于落成的铜铸圣女林昭、张志新双雕左右；双雕正中,花骨朵组成的黄色十字架上，也燃烧着一簇簇百合：这一切，都被神祭日特有的阴翳衬托得格外圣洁，格外晶莹，格外灼目灼心。<br />
<br />
青铜精铸的林昭、张志新双雕开光典礼，就此开始。<br />
<br />
四个月前――五四九十周年行将过去的5月2日，作为精神中国对五四百年最杰出的女儿经典的纪念之一，作为张志新纪念35年来最凝重的形式，严正学、朱春柳夫妇刻圣女林昭、张志新双雕，曾以纯白玻璃钢胸像的形态，在这铁玫瑰园举行过隆重的揭幕仪式。<br />
<br />
此刻，丽日金秋的铁玫瑰园，缀满猕猴桃与红石榴的青藤碧叶之下，不仅青铜浇铸的林昭、张志新胸像与钢雕底座更浑然一体，而且林雕底座的血衣，张雕的断琴及其不断的琴弦，乃至张志新母亲为女儿空冢的题诗，也全都换置安装成了青铜浇铸的了。比之玻璃钢的，这浑厚的青铜双雕岂仅更加肃穆，更加庄严，更加凝重，更加经典，而且真可谓名副其实的“永恒”了――<br />
<br />
永恒的悲悯，永恒的执着，永恒的尊严，永恒的人格：中华圣女永恒的孤独、决绝、呐喊与凝望！<br />
<br />
最该九九神祭今日，向渎神者的永恒致敬，为大悲悯的永恒追缅。<br />
<br />
十一时九分九秒：<br />
<br />
雕刻家客厅中的追思会暂停；所有与会者又都肃立圣女双雕前；唯有中国一九五七年学――五一九学奠基者、白发苍苍的钱理群教授独立在铁玫瑰园台阶上，炯目晶莹，洪声庄严：<br />
<br />
“我宣布：林昭张志新青铜双雕开光！<br />
<br />
――由甘粹先生，王书瑶先生为林昭铜雕开光!<br />
<br />
――由张志勤女士、杜光先生为张志新铜雕开光!”<br />
<br />
即使肃立者尽人皆知：甘粹先生就是林昭曾经的恋人，张志勤就是张志新的二妹，他们中大多数却未必知道杜光教授就是林林彤彤父亲（曾真）的北大同学，更未必知道：那个五一九追梦群与卫道士们短兵相接的5.22之夜，林昭为张元勋等新五四前驱辩护犹酣，一个急于声援与亮相“愣头青”却早已跃身其后了――那正是今日“开光”之王书瑶先生！<br />
<br />
黑丝绒飘然而落，诗人阿尔庄严朗诵起张元勋先生《写给林昭、张志新铜雕立雕日》。这是两节散文诗。那从岁月与心魂深处――从《广场》血涛与泪海中奔涌出来的至爱与至痛，正是双雕前肃敬着的每一个人几乎每一个九月九日的思念、愤恨与祭祈啊：<br />
<br />
“致林昭、张志新――<br />
<br />
 你和祖国母亲的土地一样，留下了被蹂躏民族的羞耻！谁能把它掩盖得不让历史遗其臭？<br />
<br />
正是为了这个混沌的民族，你的鲜血流着正义的光荣，也书写了一代暴虐的罪恶！<br />
