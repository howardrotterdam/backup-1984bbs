													<h2>王荔蕻：林昭、张志新铜像开光仪式记</h2>
						
						
																											<div id="postmessage_825322" class="t_msgfont">林昭、张志新铜像开光仪式记&nbsp;&nbsp;<br />
 <br />
作者：王荔蕻 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp;&nbsp; &nbsp;2010年9月9日9时9分，在北京市昌平区严正学先生的铁玫瑰园，庄重地举行了《林昭、张志新铜雕开光仪式》。钱理群教授主持了开光仪式，由甘粹先生、王书瑶为林昭铜雕开光。张志勤女士、杜光先生为张志新铜雕开光。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 出席典礼的嘉宾有：杜光（中央党校教授著名学者）、张志勤（张志新烈士的妹妹）、甘粹（林昭烈士的恋人）、王书瑶（前右派、著名学者）、钱理群（北京大学教授）、姚监复（中国管理科学院农业经济技术研究所研究员）、夏业良（北京大学教授）、王西麟（著名音乐家）、俞梅荪（著名学者）、朱毅（著名社会活动家、林昭张志新铜像的策划人）、严正学（铜像雕塑艺术家）、老鬼（著名作家马波）、刘真（著名社会活动家）、张丽娜（老鬼夫人）、张朗朗（美国华文作家）、王荔蕻（维权人士）、阿尔（北京诗人）、张志新的侄女、天天等约三十人。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 开光仪式前，大家进行了简短的座谈。朱毅先生主持座谈。朱毅先生说，海淀国保找过他，警告不许打横幅，在家里也不许，所以今天没有横幅，有点遗憾。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 张志新的妹妹张志勤首先发言，张女士激动地说，我代表张志新烈士的亲属，向为林昭张志新烈士雕像操劳的朋友、向对烈士满怀感情不辞辛劳的艺术家，向前来参加活动的朋友特别是老人家们表示衷心的感谢和崇高的敬意。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 张志新是为追求真理而献身的，她的事迹感人至深，一直活在人们的心中。她的伟大之处是她是在文革正处在高潮时，就提出疑问，为了坚持信念不惜付出生命代价。张志新在狱中写的《谁之罪》，她作词作曲，“今天来问罪，谁是领罪的人，我是无罪的人，我相信不久的将来，历史会告诉他，谁是领罪的人。”相信历史会告诉我们谁是领罪的人。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 杜光老今天穿着纪念陈光诚的T恤来的，他说，这两座铜像安放在铁玫瑰园，对于我们争取民主自由的人们来说是有重大意义的事件。而且放在这里非常合适，玫瑰芳香、但是有刺，加上又是铁的，正是反映了林昭张志新的铁骨铮铮。我不久前把《杜光文存》送朋友时题字：为文当如利剑良药、为人当有铁骨仁心。<br />
&nbsp; &nbsp; 林昭张志新在专制暴力面前不低头不屈服；她们既有铁骨也有仁心。我认为仁心既是中华民族传统珍宝，又有普世价值意义。仁心包括两个方面，一是对国家民族前途抱着非常热烈的情怀，同时对人类又抱着仁爱之心。她们两位就是抱着仁爱之心，坚持她们的观点、理想，面对专制暴力，不屈不挠地斗争。这是值得我们学习的。这让我想起刘晓波的话，我们没有敌人――我们的敌人是专制制度，而不是个人。抱有仁爱之心的自由派、民主派的朋友，都应该具有“我们没有敌人”的宽阔胸怀。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 我们不但要纪念林昭张志新，而且要关注和支持继承了林昭张志新精神的人，比如陈光诚。陈光诚是一个盲人，自强不息，学习了法律，为他人维权，因为揭露了当地的黑幕被关押、判刑，今天是他出狱的日子，我今天特意穿了他的文化衫，向他表示敬意。陈光诚是继承林昭张志新精神的出色代表。只有让林昭张志新的精神发扬光大下去，我们民族才有希望。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 王西麟说，我写了好些批判现实的文章，包括《什么叫主旋律》，《反思东方红》但是发不出去。现在为什么满脑子都是红歌，听说连北岛怀旧唱的都是文革中的红歌。红歌不是音乐，是政治口号。这些红歌对推广文革、造成个人崇拜起到的作用胜过千军万马。马雅可夫斯基说，歌和诗是炸弹和旗帜。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 最近我写了一部大型钢琴协奏曲，因为我对《黄河钢琴协奏曲》有看法，《黄河大合唱》是冼星海1939年写的，本来是全中国人民抗日的象征，1949年以后，变成了中共的文化标签。成为是只有共产党一家抗日的文化象征。历史的文化变异当然不由作者负责，但是大家应该知道历史事实。尤其是第四乐章，竟然把东方红加入进去，这就把抗日说成是毛一人的战争了。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 我有一个钢琴老师叫做陆洪恩，就是文革被枪毙的。陆先生说不是贝多芬要向工农兵学习，而是工农兵要向贝多芬学习。在监狱受尽折磨被杀害了。比林昭还早两天。所以林昭张志新不是一个两个，是一群人。我们纪念她们就是要改变把这么优秀的人才杀害的环境。 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 姚监复说，感谢严正学夫妇继承了林昭张志新的精神，敢于在这么大压力下，这么困难条件下进行雕塑，非常不容易。上次揭幕我没来，是因为中央维稳办找到我们部里，说不许出来吃饭。我不知道现在21世纪、堂堂这么一个大国，吃顿饭怎么就这么难，惊动了这么高的领导。不可思议。昨天半夜又通知我不许来，我想了想还是来了。……自主型变革是多种参数影响的。最决定性的影响是最慢的一个慢变量，不是政治、经济而是文化。如果文化没变，阿Q式的革命还会重新上演。无论用什么口号，出来还是一个专制帝国。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 钱理群说，林昭张志新是我们民族的圣女，和林昭张志新同时的还有陆兰秀、李九莲、钟海源等，林昭和兰州的朋友一起办《星火》杂志，他们承受的压力很大，和林昭同时被枪毙的还有一个县委书记，还有张春元，张春元的未婚妻一直在找《星火》杂志，非常幸运的是还找到了。我在想，为什么在民族苦难的时候都是这些女性挺身而出？这背后有什么原因？在我们民族妇女是受难最深重的，同样女性右派比男性右派受到的磨难也大得多，她们反抗也是最英勇的。我认为，妇女的精神也折射出一个民族的精神。所以妇女的解放是这个民族解放的一个标志。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 夏业良说，林昭张志新是先行者、也是殉道者，她们早先都是共产党的追随者，后来痛苦地发现，她们所追随的不是他们所期望的，所以她们痛苦反思、发出质疑，走上了通向炼狱的路途。她们追求的是自由、民主、平等、人权。我想问的是，当代知识分子还有哪些事情没有做？一百年来，五四运动来追求的德先生赛先生实现了吗？中国是现代化国家吗？我认为，中国是一个前现代化国家，而不是现代化的国家，因为它没有一个现代意义上的宪法、它也不尊重基本的人权，和自由的价值理念。当日本明治维新时，（虽然有戊戌变法，也失败了。）我们失去了一个时机，一个从近代到现代的转型。我们一直遵行的是改良的路径，而不是变革的、彻底变革的路径。……其实我们真正的希望在于宪政、民主、自由。<br />
 <br />
&nbsp; &nbsp; 王书瑶、俞梅荪也在座谈会上讲话。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 诗人阿尔在开光仪式上朗诵了张元勋先生的纪念文字。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; “《致林昭》――你和祖国母亲的土地一样，留下了被蹂躏民族的羞耻！谁能把它掩盖得不让历史遗其臭？<br />
&nbsp; &nbsp; 正是为了这个混沌的民族，你的鲜血流着正义的光荣，也书写了一代暴虐的罪恶！<br />
&nbsp; &nbsp; 中国伟大的女性啊，令手握权杖的独裁者，令民之贼、国之盗丧胆颤栗！<br />
&nbsp; &nbsp; 怀念烈士！大地也在为你潇潇细雨！”<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 以上讲话简单整理，如有错误，以作者整理为准。<br />
