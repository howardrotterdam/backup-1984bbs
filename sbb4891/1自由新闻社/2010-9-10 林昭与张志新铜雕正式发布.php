 <br />
致祖国――<br />
<br />
林昭殉难已四十二年了，张志新就义也35年了，尽管那淋漓的鲜血如今已变得淡红，但依然昭示着：<br />
<br />
“一是当局者竟会这样的凶残，一是流言家竟至如此下劣，一是中国的女性临难竟能如是之从容。”“苟活在淡红的血色中，会依稀看见微茫的希望：真的猛士，将更愤然而前行！”（鲁迅：《纪念刘和珍君》）<br />
<br />
怀念林昭、张志新，我们想起了这些！”<br />
<br />
