<br />
杜光先生是穿着印有盲人律师陈光诚的文化衫率先来宾发言的，耄耋老人的如此装饰连同他感言中一如揭幕典礼上的针砭、犀利与激情，真是一派高山仰止的精神风景！感言者无不盛赞严正学、朱春柳夫妇排除万难雕刻圣女的担当精神与精湛奉献。作曲家王西麟先生对民族抗日圣歌的黄河交响曲被主流意识形态化、标签化的抨击，王书瑶先生对民主社会主义理论之乌托邦质疑与透析，姚监复先生对于专制钳制时代精神与社会活力的揭露与感慨，钱理群先生对浩劫中林昭、张志新、李九莲、钟海源、陆兰秀之“烈女现象”与知识分子犬儒化蜕变心程的的深层追问，夏业良教授直面血史与现实对“人民”的质疑与对自由、宪政、公民社会和普世价值的推崇与疾呼，都是圣女双铜雕开光日追思的亮点。这类价值深层的交流，一直绵延到正午的餐桌上。<br />
