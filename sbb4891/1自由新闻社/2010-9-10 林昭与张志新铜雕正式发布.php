													<h2>杜光教授在林昭、张志新铜像正式发布仪式上的发言</h2>
						
						
																											<div id="postmessage_825317" class="t_msgfont">继承和发扬铁骨仁心的崇高品格<br />
―― 在林昭、张志新铜像“开光”仪式上的发言<br />
杜光<br />
<br />
 <br />
我们今天在这里举行林昭、张志新铜像的“开光”仪式，首先要感谢严正学、朱春柳两位艺术家夫妇，他们在完成林昭、张志新的雕塑后，短短四个月里，排除种种干扰，克服许多困难，终于完成了这个意义重大的工程，这是很值得庆贺的。<br />
<br />
他们决定把这两个铜像无偿地分别赠送给林昭和张志新的母校――北京大学和中国人民大学，安放在校园里。在这两所大学同意接受以前，先设置在他们的小花园――“铁玫瑰园”里，供公众瞻仰。<br />
<br />
在她们的铜像回到母校前，先安置在铁玫瑰园里，可以说是意味深长。林昭、张志新生前的精神和铜像所蕴藏的内涵，就像美丽的玫瑰，枝条上长满了刺，而且是铁的，任何人都无法折断它，摧毁它。它象征着这两位女杰的铁骨铮铮的崇高品格。铜像和铁玫瑰园融为一体，成为铁玫瑰园的灵魂。<br />
<br />
林昭和张志新面对专制暴力，不屈不挠，宁折不弯。但另一方面，她们对祖国，对人民，却充满了仁爱之心。林昭在她的血书里呼唤自由，呼唤人性，并且把人性归结为“仁心”，确是她的肺腑之言。<br />
<br />
我不久前为一位朋友在《杜光文存》上题字：“为文当如利剑良药，为人当有铁骨仁心。”林昭、张志新就是铁骨仁心的典范。<br />
<br />
仁心，既是我国的优良文化传统，也是普世价值。“仁者爱人”是儒家思想的精华，据有人统计，在《论语》里，“仁”字出现过109次。《儒家文化的人学视野》一书的作者王杰认为：“‘仁’所表现的是一种服务于‘理想人格’的境界”，这是很有见地的。对理想人格的追求是人类共有的天性，所以，仁心的孕育、培养、发挥，也就具有普世的意义。关于仁的思想，是我国传统文化对普世价值的重大贡献。<br />
<br />
仁心表现为对祖国和民族的热爱和忠诚。文天祥的《正气歌》里有一个传诵千古的名句：“留取丹心照汗青。”这里的“丹心”也可以理解为“仁心”。张志新和林昭正是因为对祖国和我们民族的热爱和忠诚，坚持真理，才受到专制暴力的长期摧残，并最终遭到杀害。孔子在《论语・卫灵公》里说：“志士仁人，无求生以害仁。有杀身以成仁。”张志新和林昭就是这样杀身成仁的仁人志士。<br />
<br />
林昭、张志新在专制暴政的残酷迫害面前铁骨铮铮，对祖国和亿万同胞却柔情似水，深怀眷爱。林昭1966年在监狱里对探望她的张元勋说：“如果有一天允许的话，不要忘记告诉活着的人们：有一个林昭因为太爱他们而被杀掉。”只有大仁大爱的人，才能达到这样的境界。林昭和张志新都是铁骨仁心的楷模，我们为她们建立雕塑和铜像，不仅是为了纪念她们，更重要的是要继承和发扬她们那种铁骨仁心的高尚品格，为在中国争取自由民主的伟大事业作出贡献。<br />
<br />
在维权人士的队伍里，有许多继承、发扬着铁骨仁心的精神和品格的人物，陈光诚就是他们的出色代表。陈光诚自幼双目失明，靠自修法律而成为律师。从1996年起，他就为残疾人和乡民免费提供法律咨询，受到人们的爱戴，享誉海内外。2002年3月，美国《新闻周刊》以封面故事介绍了他维护残疾人权益和建立乡村法律图书馆的梦想。2003年，临沂市把他评为“十大新闻人物”之一。2005年12月，香港《亚洲周刊》发表文章，赞誉他是“2005年风云人物”。2006年5月，他又被美国《时代周刊》选入“塑造世界的100人”，中国入选者除他外还有温家宝等4人。<br />
<br />
2005年春天，陈光诚根据乡民的反映，调查临沂地方政府在计划生育工作中的种种野蛮的违法行为，并在网上公布，引起了国务院计生委的重视，一些有关官员因此受到查处，陈光诚却遭到残酷的报复。他在被软禁7个月后，2006年3月被沂南县公安局拘留，6月以“毁坏公共财物”、“聚众扰乱交通秩序”等罪名，被判处四年又三个月徒刑。今天正好是陈光诚刑满出狱的日子。昨天有一位维权律师送给我一件印有陈光诚头像的文化衫，今天我就穿着它参加林昭、张志新的铜像揭幕式，因为他是林昭张志新的铁骨仁心的精神和品格的继承者，同时也想借着这个机会，同大家一起祝贺他今天重新获得自由。他在专制暴力面前顽强抗争，坚定不屈，对权益受到侵害的弱者，却充满了仁爱关切之情，甘愿为帮助他们讨回公道而备受打压，坐穿牢底。他的品格和精神，同样值得我们学习。铁玫瑰园的铜像同陈光诚的心是相通的。<br />
<br />
2010年是民间民主运动的潜流汹涌澎湃的一年，也是社会发展充满变数的一年，形势扑朔迷离，令人难以捉摸；遥望前景，凶险与乐观并存。处在这个特殊的情景里，我们更应该继承和发扬铁骨仁心的精神，一方面，坚定不移地推进宪政民主运动、维权运动和再启蒙运动，在专制暴力面前百折不挠；另一方面，要抱有一片仁爱之心，以宽容、宽厚、宽松的胸怀去待人处事，推动、建立广泛的联合阵线，用争取自由民主的新进展、新成就，回报林昭、张志新为我们所作的牺牲。<br />
<br />
（根据9月9日揭幕式上的即席发言整理，有所增删）<br />
<br />
