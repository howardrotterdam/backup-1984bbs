																											<div id="postmessage_304546" class="t_msgfont">[事发]长发被剪，女生哭着回家<br />
<br />
　　被剪掉长发的女生叫晶晶(化名)，据她的舅舅王先生介绍，她刚从小学六年级升入初中一年级。前些日子，学校整顿校纪校风，要求学生剪短头发。晶晶的头发留了多年，她很舍不得剪掉长发，并曾告诉父母“不愿意剪头发”。<br />
<br />
　　前日下午，晶晶被班主任叫到办公室门口。班主任拿出一把剪刀，几下就把晶晶的头发剪得乱七八糟。王先生说，晶晶回到教室后，遭到同学们的取笑，说她的头发太难看了。当天下午放学，晶晶是哭着回家的。<br />
<br />
　　晶晶回到家后，母亲看到女儿的头发参差不齐，就问缘由，委屈中的晶晶不说。“以为她做了什么坏事，她妈就打了她几下。”晶晶的亲属说，晶晶被打后才说出被老师强行剪掉长发的事。晶晶表示想退学，不敢再去学校，怕同学取笑。<br />
<br />
　　王先生听到外甥女的遭遇，勃然大怒：“现在都什么年代了，她(班主任)还这样做？如果要剪头发，你找家长沟通呀，又不找。”他认为，女孩子留长发是正常的，13岁的晶晶正是爱美的年华，头发长一些无可厚非。<br />
<br />
　　[进展]校方道歉，已获家属接受<br />
<br />
　　昨天，王先生找到光祖中学的领导，反映了晶晶被强行剪掉长发的事，校方和班主任刘老师向他道了歉。<br />
<br />
　　记者昨日到光祖中学采访时，王先生表示在与校方沟通后，已经接受校方的道歉。该校一名姓李的老师告诉记者，晶晶的班主任刘老师缺乏经验，管教学生的心情太急切，所以酿成了这次事件。他表示，以后学校不会再出现类似事件，如果学生头发太长，校方会先与学生家长沟通，让家长跟学生讲道理；如果学生仍不愿意，校方也不会强行剪掉学生的头发。<br />
<br />
　　晶晶的班主任刘老师看上去压力很大，她不太愿意述说当天的情形，只表示已经很诚恳地向家长道歉。<br />
<br />
　　在光祖中学，记者没有见到晶晶。校方表示，晶晶已被家长带走。<br />
<br />
　　记者随机采访了多名学生。一名女生说，学校对女生的头发有规定，额头处不能盖住眉毛，两侧不能盖住耳朵，如果被发现头发过长，学生所在班级就会被取消获颁流动红旗的资格，涉事学生的德育分也会被扣掉。<br />
