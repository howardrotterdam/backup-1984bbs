<br />
　　近年来，“9・11”阴谋论很流行。包括在美国，一批对这一恐怖袭击事件心存怀疑的人定期举行集会和会议，寻找事件真相。<br />
<br />
　　他们认为，“9・11”事件真相也许是：那是美国政府为引起世界战争一手导演的阴谋。<br />
<br />
　　古巴领导人卡斯特罗也曾在其发表的《总司令的思考》系列文章中写道：在“9・11”事件中撞向五角大楼的不是飞机，而是一枚火箭，因为事后没有找到乘客的残骸。<br />
