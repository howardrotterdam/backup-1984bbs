<br />
下午5时，与这名“扁担”同族的远亲匆匆赶来。“他中午吃饭时还跟我说，这体力活实在太辛苦，担心往后身体吃不消，还在盘算着买辆电动车改行。这是他跟我说的最后一句话，没想到，他就这么去了。”这名同样在汉正街靠肩挑背扛为生的汉子哽咽着。<br />
<br />
据了解，这名加姓“扁担”平时在随州务农，两个儿子都在广东打工，家境贫困。为挣钱养家，他不时利用务农的间隙来汉做“扁担”，但此前，他的身体没有大病。<br />
<br />
（荆楚网-楚天都市报 张欣 冯毅成）</div>

							
							
							
							
