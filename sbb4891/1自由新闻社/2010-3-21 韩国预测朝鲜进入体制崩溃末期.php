													<h2>北韩局势不同寻常 体制崩溃倒计时？</h2>
						
						
																											<div id="postmessage_524733" class="t_msgfont">襄阳=朝鲜日报记者 安勇炫 (2010.03.20 09:46)<br />
<br />
北韩的货币改革以失败告终后，美国国务院助理国务卿坎贝尔预测说北韩国防委员长金正日的寿命“可能只剩下三年”。在这种情况下，各方纷纷担心，如果这种状况持续下去北韩体制崩溃可能会进入“倒计时”。<br />
<br />
民主和平统一咨询会议（简称民主平统）和梨花女子大学统一学研究所19日在江原道襄阳大明度假村内共同举办了“南北关系专家大型讨论会”。在讨论会上，西江大学教授Kim Yeong-Su主张说：“北韩现在正在一点点消耗可以坚持的时间。”<br />
<br />
北韩体制从今年年初开始“脱离原先轨道”，因此有言论称：“今年之内北韩局势很有可能发生前所未有的‘变化’。” Kim Yeong-Su表示：“正如货币改革失败所体现的那样，北韩对市场的依存度已经非常高，信息流通速度也很快，不断出现深有意义的变化。”北韩老百姓的认识发生了翻天覆地的改变，南韩化妆品和电饭锅已成为众所周知的“名品”，甚至连电视剧《IRIS》和《善德女王》也非常受欢迎，而且，军队状况也和过去截然不同，不断发生新兵逃离军营的事情。他还表示：“现在必须要防止即将倒下的枯木（北韩）倒向南方。”<br />
<br />
当天，庆南大学教授柳吉表示：“北韩发生剧变的可能性至少超过20%。”他强调，像以前那样试图通过经济合作和援助促使北韩体制发生变化的想法是一种奢望，但要让北韩‘快点崩溃’的态度也超出我们的能力之外，因此，“应该制定现实的（剧变）对策。”高丽大学教授柳浩烈表示：“北韩今年还会运用悬崖外交和传统的欺骗战术，但如果同美国之间的对峙局面恶化，金正日可能会丧失统治力。”统一研究院博士朴s重等专家主张说：“金正银继承权力最终会失败。”就北韩今年面临危机的根据，专家指出了4到6月春荒期至少缺粮50万吨、南韩和中国等外部援助不充分、国民对金正日的不满情绪迅速扩散等。<br />
<br />
相反，庆南大学教授金根植表示：“我们决不能凭借北韩很快会灭亡的主观臆测(wishful thinking)，把所有精力放在对北政策上。政府针对北韩的不稳定状况制定对策固然重要，但也要针对北韩恢复稳定的情况做准备。”<strong>他指出，如果北韩得到中国的援助，就不会马上发生剧变事态，</strong>如果一味对北韩施压，只会导致其采取强硬手段。<br />
<br />
当天，曾担任北核六方会谈韩国代表团团长的前国家情报院第一次长李秀赫在首尔三[洞庆南大学远东问题研究所举行的座谈会上表示：“虽然六方会谈进展不顺利，经常受挫，但没有可以替代六方会谈的对策。现在要做好准备应对北韩剧变。”<br />
