&nbsp; &nbsp; 题：Xi Jinping总结中共“五个必须” 网民称有现实意义<br />
<br />
　　中新社记者 德永健<br />
<br />
　　中共中央政治局常委、中央书记处书记、中央党校校长Xi Jinping在新中国成立将满六十年之际，总结出中共在六十年间的两大转变和党建工作五大启示。内地网民称，Xi Jinping的讲话意味深长，富有很强现实指导意义。<br />
<br />
　　Xi Jinping一日在出席中央党校二00九年秋季学期开学典礼时发表上述讲话。一日晚的中央电视台《新闻联播》以及二日出版的《人民日报》头版均播发或刊登，称“Xi Jinping在讲话中围绕新中国六十年党的建设谈了一些思考和认识”。<br />
<br />
　　Xi Jinping在讲话中总结六十年间中共两大转变，指出第一次转变以一九四九年新中国成立为标志，中共从一个领导人民为夺取全国政权而奋斗的党，开始向一个领导人民掌握全国政权并实现由新民主主义过渡到社会主义的执政党转变。<br />
<br />
　　第二次转变是在中共十一届三中全会以后，中共从一个受到外部封锁和实行计划经济条件下领导国家建设的执政党，开始向对外开放和实行社会主义市场经济条件下领导国家建设的执政党转变。<br />
<br />
　　Xi Jinping同时总结六十年来中共党建工作五大启示，并以“五个必须”加以阐释，包括：<br />
<br />
　　――必须坚持把马克思主义基本原理同中国具体实际和时代特征相结合，坚定不移地走中国特色社会主义道路；<br />
<br />
　　――必须紧紧围绕党的中心任务尤其是发展这个党执政兴国的第一要务来加强和改进党的建设，确保党建工作始终服务于经济社会发展；<br />
<br />
　　――必须坚持立党为公、执政为民，把实现好、维护好、发展好最广大人民的根本利益作为党的建设必须始终遵循的宗旨、方向和目的，始终保持党同人民群众的血肉联系；<br />
<br />
　　――必须坚持和健全民主集中制，发展党内民主，维护中央权威，不断增强党的蓬勃活力和团结统一；<br />
<br />
　　――必须以党的执政能力建设和先进性建设为主线，以改革创新为动力，推动党的思想建设、组织建设、作风建设、制度建设和反腐倡廉建设相互促进，从整体上提高党的建设水平；<br />
<br />
　　Xi Jinping的讲话见诸媒体后，有网民在人民网等官网留言，认为在庆祝新中国成立六十年之际，Xi Jinping强调要加强党建的讲话显然意味深长，富有很强现实指导意义。<br />
