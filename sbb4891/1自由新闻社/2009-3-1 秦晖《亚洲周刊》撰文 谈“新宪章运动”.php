													<h2>秦晖《亚洲周刊》撰文 谈“新宪章运动”</h2>
						
						
																											<div id="postmessage_42353" class="t_msgfont">中国更需要民主辩论与重新启蒙<br />
<br />
秦晖<br />
<br />
亚洲周刊2009年第9期<br />
<br />
秦晖认为，当下中国民间社会共识破裂并深刻分化，改革的一些得利者迷恋弄权致富，不希望民主；而改革的一些受损者出现反改革情绪，希望文革式民主。今天的中国需要一场关于民主的思想论战与启蒙，更胜于签名呼吁民主。<br />
<br />
去年十二月二十四日起清华大学有关方面几次给我打电话说，「保卫部门」要找我谈话。到今年一月九日，人文学院的书记、副书记与我约定后来到寒舍，对我说：上面知道我签署了「08xz」，因此让他们来调查情况。<br />
<br />
我对他们说：我并未签署「08xz」。说我签了那是误传。但是「宪章」的主要精神和诉求我是赞同的。签署者中也有我的很多朋友。很久以来只要有机会，议论中国的宪政民主进程应当如何启动就是常有的话题，也包括讨论各种各样的文本。在人们思想交流如此活跃、交流手段也如此多样的信息社会，郑重其事地查问谁在什么时间同你第几次讨论某个文章，这不是很无聊吗？既不涉及国家机密，也不涉及刑事案件，调查这些是要干什么呢？<br />
<br />
我表示：由于对文本内容等方面有些看法，我没有签署这个文件。但我不能承诺以后也不会签署类似文件。要求公民只能表政府喜欢的态，这在文明发达的今天是很丢脸的事儿。不要说我对「宪章」的主要精神是赞成的，就是不赞成，我也持「不同意你的观点，但坚决捍卫你发表观点的权利」的立场，反对因「宪章」之事抓人。我没有在08xz上签名，但如果有人发动签名要求释放因「宪章」被捕的刘晓波先生，我很可能签名。因为如果今天因言论可以抓刘晓波，明天也可能因言论而抓我。捍卫刘晓波的言论自由，也就是捍卫我们自己、包括那些不赞成刘观点的人的言论自由。<br />
<br />
那么我为什么没有签署「08xz」？我想「08xz」意在仿效当年捷克的「七七宪章」，其意向很好。但两者面临的局势有很大不同，两者的文本性质也大有区别。以不同性质的文本在迥异的背景下发动类似的签名运动，效果未必好。原因在于：<br />
<br />
第一，民主化在捷克本来就比在中国有更深厚的土壤：捷克在二战前已是中东欧最成熟的宪政民主国家，中国自晚清以来宪政民主却迄未成功。捷克六八年「布拉格之春」在自由民主诉求下又加上因苏军侵占引起的民族情感，其全民参与的深度广度也是我国一九八九年民主运动无法比的。而我国却没有任何一个守旧派因八九年运动而失势，倒是赵紫阳系统全军覆没，镇压者甚至用不着在其中寻找叛徒。<br />
<br />
第二，一九六八年后的捷克回到铁幕状态，到七七宪章时并无任何改革，经济、政治体制同样僵化。人民的普遍不满和思变就如文革结束时的中国，因此当时妨碍人们支持七七宪章的基本上就只是对镇压的恐惧。而中国在八九年后虽然政治上停止了改革，经济上却出现了明显转轨，市场经济、全球化带来的经济奇迹造成的社会不公早已使民间社会「共识破裂」，并形成深刻的分化。经济改革中的一些得利者尝到了弄权致富的甜头，他们并不希望民主，而改革中的一些受损者却出现了反改革的原教旨主义情绪，他们希望的「民主」是文革式的，与08xz的自由取向南辕北辙。因此可以说，今天妨碍中国人支持08xz的已经不止是恐惧，对不少人来说甚至可能主要不是恐惧。今天在中国提出宪政民主的主张是需要论证的，并不是一批勇敢者的表态就可以激励起民主运动的士气。可以说，今天中国之需要一场深刻的思想论战，远过于需要一场签名。<br />
<br />
第三，七七年时捷克和整个苏东阵营处于「停滞时期」，计划经济名声扫地。而西方已经走出石油危机和布雷顿森林体系解体危机的阴影，处在经济繁荣期的起点，并且由于此前的滞胀使凯恩斯主义受挫，经济自由主义势头渐兴，七七年后不久就进入了「撒切尔―列根繁荣」。在西方这与民主发展谈不上有什么关系（因为他们的左右派都是要民主的），但在东欧，由于自由经济与计划经济的对立，全球经济自由化的大潮也利于他们政治上民主潮的推进。<br />
<br />
但是二零零八年恰恰相反，世界陷入了华尔街引爆的严重经济危机，形势有如一九二九年后的大萧条时代。尽管今天危机的发生机制与一九二九年完全不同，但「一九二九年心态」仍有严重影响，这种心态往往倾向于呼唤铁腕救世，导致民主自由退潮、极左极右两种专制得势。大萧条时代欧洲不但一战后出现的一大批新兴民主国家，除了捷克全部都「重返专制」，就连一战前宪政已有长足进步的德意两国也法西斯化、纳粹化了。<br />
<br />
警惕一九二九心态<br />
<br />
我并不认为二零零八等于一九二九，但今天除了国家干预成为时髦、经济自由饱受非议外，为危机所困扰的国际社会对中国的期望也主要是反危机政策的配合，而不是民主化。因此近期中国的民主力量实际处于守势，当务之急是应对危机挑战，以民主人权思想来解释危机现象并指出正确的反危机方向，防止出现法西斯化。此时照搬捷克七七年的经验，重复常态下的民主口号并以此摆出攻势姿态，效果将不会好。可以说，如果我们不能解决危机时期人们的认识混乱，已经民主了的国家都会选出希特勒，何况在不民主的国家开创民主？<br />
<br />
因此，我觉得，在现在的中国发动类似于捷克七七宪章那样的行动未必适宜。当然从另一方面讲，越是不利民主进程的形势下，越应有人出来大声疾呼，抵制犬儒主义，振奋民主精神。应该说「08xz」公布后的确在这方面显示出作用，刘晓波先生等人表现出来的道德勇气与担当精神也令人钦佩。<br />
<br />
但是从这方面来考虑，宪章的文本就特别重要。宪章公布后很多人称赞它体现了温和、理性和改良的精神，这没错，这是它与捷克七七宪章的共同优点。但中国的民主运动有自己的特点，即便在共同底线方面也是这样。<br />
<br />
例如，捷克七七宪章以实行联合国人权公约为诉求，但实际文本中大量提及的只是《公民权利和政治权利国际公约》，这在捷克是不难理解的。因为捷克作为老牌工业化国家，并没有把计划经济用作原始积累的手段，她在旧体制下已经有比较高的全民福利水平。而如果要从计划经济向市场经济转轨，高福利状态反而会成为引起争议的话题。<br />
<br />
但中国则不然，毛泽东时代的命令经济主要是一种剥夺农民的原始积累机制，它既非高福利也非低福利、而是「负福利」体制，即福利与特权挂?，不仅不覆盖最大弱势群体即农民，甚至也极少覆盖国家雇员之外的弱势市民，「二次分配」不但不缩小、而且还扩大了不平等。而中国的改革长期以来只着重于引进盈利竞争（甚至政府也唯利是图），社会保障建设却严重滞后。对于中国底层大众而言，其社会保障水平不仅不如北欧式的高福利国家，也不如美国这样被认为是「自由放任」的国家，甚至比号称全球最自由经济体的香港都差得远。而在号称同样搞社会主义的国家中，这种「负福利」也与捷克的福利制度相差悬殊。<br />
<br />
中国民众既受害于统治者权力太大，也受害于统治者责任太小。尤其在当前经济危机时期，对民主的诉求如果不与大众最基本的「经济社会权利」结合，很难指望他们的参与。而这些「最基本的」保障并不是什么高福利，它是最自由放任的民主国家也必须有的。08xz如果只谈政治权利不谈经济问题倒也罢了（前面说过，这可以理解为回避歧见），既然谈了，除了经济自由（如土地私有制）诉求外也应该有福利与公共服务诉求。后一诉求如果太高调（比如要求北欧式高福利）那就和极端经济自由一样会引起争议，但基本福利与基本自由一样，属于共同底线性质，是不应该有什么争议的。宪章文本不涉及这些，就是一个遗憾了。<br />
<br />
总之，08xz文本上的缺点在于它涉及了太多民主支持者中可能存在争议的问题，同时却回避了一些民主支持者中不应有争议的问题。<br />
<br />
当然，以上分析只是供参与宪章签名的朋友们商榷。我在宪章公布前的讨论中就已表示，我认为现在不是时候；要做，这个文本也应该适当修改。后来有的朋友说：他们签名是基于对朋友的信任，甚至没看文本就签了。我其实也相信这些朋友的为人，但对文本的态度却没有这么潇洒，这种事毕竟不只是「友情派对」，所谓「宪章」从字面上讲，签了就是要遵守的。因此我没有参加签名。<br />
<br />
一些所谓左派的朋友不同意08xz的观点，我完全可以理解，因为我本人也并不完全同意这个文本。但是像「乌有之乡」网站那样呼吁镇压宪章参与者，我觉得简直不可理喻：这些以文革派自诩的朋友难道不知道自己的主张也不符合「主旋律」吗？他们号召起来「造反」，号召打倒「党内走资本主义道路的当权派」，难道他们认为刘晓波就是这样的「当权派」？今天假如真有所谓毛派，他们如果不想搞「武装斗争」，不也只有寄希望于言论自由吗？这大概也是「马列主义毛思想」承认的大道理吧？<br />
<br />
这些朋友还说08xz参与者想搞「颜色革命」，可怜他们对所谓「颜色革命」的见解还远不如俄罗斯共产党。乌克兰「颜色革命」时，俄罗斯《真理报》网站曾发布《俄罗斯人看「颜色革命」》的民意调查，在回答「革命」原因时选择「前政权不称职与腐败」的占百分之四十，选择「美国和其它西方国家的压力」的只有百分之十六。而在白俄罗斯，反对卢卡申科的白俄共产党干脆就是当地「颜色革命」的主力。<br />
<br />
对颜色革命的误解<br />
<br />
其实，这就像零三年美国推翻萨达姆政权后，正是曾遭到萨达姆残酷屠杀的伊拉克共产党成为首先出来与美军合作的派别之一（伊共总书记第一时间就参加了与美方合作的「六人委员会」）、以及当年国民党统治下中共呼吁美国向蒋介石施压，甚至要求「每个在华美军士兵都成为民主的宣传员」一样，很容易理解：这些共产党人既谈不上「亲西方」更说不上喜欢「资本主义」，但他们在别人的专制下都感到受威胁，都知道人权的重要，都懂得首先争取自由，然后再讲其它。但推己及人，别人这么想为什么就不行？<br />
<br />
如果我们不希望中国也发生所谓「颜色革命」，更不想让别人插手中国的事，那么就让我们自己来推动民主化吧。正如谭嗣同当年说的：「华人不自为之，其祸可胜言哉？」</div>

							
							
							
							
