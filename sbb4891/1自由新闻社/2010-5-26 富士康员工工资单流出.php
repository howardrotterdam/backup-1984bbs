深圳市总工会也在有关报告中说，未发现富士康存在强迫加班、严重超时加班等违反劳动法规现象。不知道工会的报告是 富士康交给她们的 还是她们自己去采集的。就会忽悠社会忽悠人民！我08年进富士康呆了一年多到去年底自离的，辞工基本上要一个多月才能批下来吧，还得看主管的心情了。每个月加班都上了一百多个小时的，一个月只休息2天，是调休的，也就是说安排你每月1号16号休息，如果1号是星期1-星期5其中一天 就会用你星期6星期天的8个小时加班2来扣。每天工作时间是11小时+吃2顿饭1小时 在厂里要呆12小时以上，比如早上8点上班 7：40就要开早会，那么我们7点就要起床 出门了。晚上 正常是8点下班 不正常9点 10点都有可能。而且8点下班了 还强迫要求义务工作10分钟 搞5S（就是搞卫生）。过年后2个月就苦了，同样每天工作11小时，每月休息2天 但是加班2都被扣了，要抵年假。<br />
我们每个月底都要签一份自愿加班确切书，其实只是做做样子而已，你不签字上的线长组长就怕完不成任务会帮你签了！而且你不签还要被骂，不能得罪上面，要不工作上吃亏的也只是自己的！所以大家一般都会看都不看的签了的 都习惯了！ 实在太累了要想休息请事假是不可能的，怎么办？我们一般就会跑到社康中心去找医生开个病历表拿去请病假的，一年只有 15天这样的特权，所以我们都会很节约的运用这15天的病假！还有很多事我一下也说不清楚的，文笔不好 没发表达出来。<br />
