<br />
　　王菲：复出造型致敬时尚鬼才 <br />
<br />
　　东方早报报道，今年春晚歌舞类节目里面最令人期待的就是王菲的复出和小虎队重组，许多观众都是冲着两人去看春晚，有很多观众在网上留言说看完王菲和小虎队就可以睡觉去。昨晚第10个节目就是赶着回家陪孩子倒数过年的王菲，她穿着刚刚自杀的英国时尚鬼才亚历山大.麦昆设计的碎花小礼服惊F登场。据春晚内部人士告诉记者，原本王菲准备了两套礼服，录制备播带那天王菲造型曝光以后，外界的评价是比较中性，一向喜欢追求独特的王菲原本想换造型，没想到第二天却传出麦昆自杀，王菲决心向自己崇拜的设计师致敬，她决定不再换造型。昨晚王菲整身粉色造型亮相以后，有网友指出这造型是抄袭张惠妹2007年专辑《A　MEI　STAR》中粉丝袜造型之嫌。为此，记者昨天采访了上海本土设计师黄先生，他表示王菲的造型不属于抄袭，因为她的碎花裙子配粉红丝袜很适合，穿丝袜是女生都会穿，不存在抄袭，而且春晚是喜庆的场合，粉红色很配合气氛，王菲这次造型配搭恰到好处。 <br />
