<br />
这个周末和家人聊天时得知，原来我工作过的中国原子能研究院的人出了大事儿了。一个叫康日新（为了取乐我下面叫他康师傅）的伙计，一个78年毕业的“工农兵”大学生，从我们研究院开始，一点点爬到核工部当了大官（中国核工业集团公司党组书记、总经理）。近期突然爆出，康师傅贪污了7000万欧元。全部是欧元现金，藏在他家别墅的三楼上。家人说我见过他，见面也许认识。我听了这个消息吃了一惊，赶紧到网上查这个人，没错了！这个人我确实在院里见过，记不清他当时是什么职务，但遥远的记忆说；他是一个还算本分的人。好嘛！7000万欧元现金，合算人民币7亿多。更可笑的是；他是中国共产党第十六届中央纪律检查委员会委员。这东西可烂到心脏里！中央纪律检查委员会委员，家里阁楼堆着7000万欧元长时间没人知道。问题来了：谁来检查中央纪律检查委员会委员呀？ <br />
<br />
人这个东西可真邪门啊！87年我在研究院的时候，因为开发民品项目成果异常突出，我成了全院劳模中唯一的年轻人。院有关规定说；我可以从我开发的项目中抽成9%作为个人奖金。我干的项目价值十几万，9%就是1万多。1万元可是相当于我当年10年的工资啊，这是多么巨大的诱惑啊。可我立即注意到周围那一双双闪着绿光的眼睛，我估计如果我“独吞”这1万，我别想在院里混下去了。好汉不吃眼前亏啊，我主动提出拿8000元给全研究室的所有人“喝汤”。这个行动给我带来巨大的人缘，现在想想如果没有那8000元“贿赂”全研究室，我不可能有来荷兰的机会。更不可能变成荷兰人了。 <br />
<br />
这个康师傅可以肯定家里拿不出来我那2000元存款；他78年工农兵大学生毕业（所有工农兵大学生按大专处理），我83年科大毕业。他的工资和我应该不相上下。刨除家里老老小小的吃喝，能有几百元的存款就撑死了！这是二十年前，二十年后，在他家的阁楼里抬出7000万欧元现金！7000万欧元现金是一个什么概念？薄熙来在重庆打黑的成果展览上的那个“金山”（现金堆成一个山）也不过是区区2000万人民币。7000万欧元，相当于7亿多人民币，堆35个那样的“金山”！薄熙来得抓35个黑老大才赶上一个中央纪律检查委员会委员！现在大家看看，这个打黑除了表演外，还要什么实际意义！这个体制把一个连几百元都拿不出来的穷光蛋，在短短20年搞成7亿现金堆在家里。了不得了，这日子没法过了。 <br />
<br />
最好玩的是这个案子不是中国自己“发掘”出来的，和那个“三鹿”毒牛奶案一样，是外国人不干了！直接捅到中国政府最高层。“三鹿”事件是新西兰方面捅的，这次据说是德国方面捅的。这让我想起了大亚湾核电站。人们都以为这个项目会落到德国西门子公司手上，因为他们提前三年就投资中国核电市场，对中国的情况是最熟悉的。但招标却莫名其妙地跑到了法国人手上，把德国气得当场解雇了中国方面负责的总经理。我一直怀疑这里面有作弊，受贿行为。哦，原来是这么回事！ <br />
<br />
德国这个民族最讨厌贿赂，他们狂妄地认为德国的技术天下第一，你“必须”买，根本不需要贿赂。他们认为贿赂等于承认自己的技术不如人家，技术不如人家是德国人认为最大的耻辱，所以德国人绝不行贿！但据说这次在秦山核电站这个项目上德国人高傲的头也被迫低下了，他们也开始贿赂了。可这位可爱的康师傅拿了人家的钱却不办事儿。德国人忍无可忍了，被逼的干贿赂这等下流勾当已经是在侮辱德意志这个高傲的民族，贿赂了还没有效果，碰上了一个无底洞。德国人的愤怒爆发了！咱们这艘可爱的“X”字牌海盗船就被炸沉了！ <br />
<br />
中国是一个能源需求到了要命的国家。世界上所有的石油给中国都不够。我二十年前还在核能领域工作的时候就预测中国需要上百座核电站缓解中国对能源的饥渴。核电站的特点是建造投资巨大，但运行成本及环境污染极低！中国建造核电站的最大障碍（除了核技术外）就是投资问题；一个发展中国家，到哪里搞那么多初始投资啊！好嘛，一座核电站还要喂给贪官1亿欧元（注意；7000万欧元是康师傅自己的钱，其它人分3000万不过分，如果康师傅不给其它人好处，他不可能保密这么长时间）。100座核电站还要给贪官准备好100亿欧元，1000亿人民币的私房钱。中国哪里找这笔钱啊。 <br />
