核电招标。其中，最有实力的竞标者，是美国西屋公司与法国阿海珐集团。同年年底，中<br />
国技术进出口公司牵头，与中核集团和中广核集团共同成立了核电招标的筹备组。2006年<br />
年底，中国核电技术招标机构选择了美国西屋公司和肖工程公司联合体作为优先中标方。<br />
 　但第二年年中，阿海珐集团获得了广东台山核电项目两台机组的订单,这一结果令业界<br />
深感意外。此后据法国媒体的报道称,在阿海珐集团(Areva)任职的保罗费尔腾在中国被强<br />
制扣留两个多月。<br />
　　不过，据知情人士称，除了康日新涉嫌核电招标事件之外，五大发电公司亦有人牵扯<br />
其中。本组稿件均综合<br />
　　曾经的精彩语录 现在看来是讽刺<br />
