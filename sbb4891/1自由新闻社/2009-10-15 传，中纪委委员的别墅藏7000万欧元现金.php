总面值为18亿元，期限5年。时隔8个月之后,中核集团于今年7月15日发行2009年度企业债<br />
券。发行金额40亿元，期限10年。本期债券所募集的40亿元资金，将全部用于福建福清核<br />
电一期工程项目、浙江秦山核电厂扩建项目(方家山核电工程)项目和三门核电工程项目，<br />
以及补充公司流动资金<br />
