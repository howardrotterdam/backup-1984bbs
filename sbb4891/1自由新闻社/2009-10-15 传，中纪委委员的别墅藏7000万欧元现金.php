了，分担给你责任，你就应该做好。”<br />
　　多面康日新<br />
　　家庭：父亲当过生产队长<br />
　　康日新出身贫苦，自他以上，三代为农。父亲是当地的生产队长，也是早年的军人。<br />
父母亲对他所进行的教育对于他后来的人生影响深远。<br />
