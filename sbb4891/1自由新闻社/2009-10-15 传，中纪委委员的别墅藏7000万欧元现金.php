&nbsp; &nbsp; 昨日，本报报道了中国核工业集团公司(下称“中核集团”)党组书记、总经理康日新<br />
因涉嫌严重违纪，目前正在接受组织调查。有相关人士称，涉及的总额达到了18亿元人民<br />
币。据悉，康日新目前被关押在北京。<br />
　　一位知情人士称，康日新此次被调查主要涉及两个问题，其一是挪用资金炒股，其二<br />
是涉嫌干涉核电招标结果。不过,据一位中核集团内部人士分析,康日新被调查可能是在一<br />
些重点工程当中出现了腐败。近期，国家加大了核电发展力度，一大批项目上马，很多项<br />
目的投资就上千万元，这中间很可能出现腐败。<br />
　　两大问题<br />
　　挪用资金炒股 一边鼓吹利润大增 一边四处融资<br />
　　在5月举行的企业国有产权管理工作暨产权交易机构工作会议上,国资委副主任李伟表<br />
示,为了防范国有企业投资金融领域出现风险,国资委今年将对中央企业投资金融领域的总<br />
体情况进行一次摸底调查，并在此基础上提出相关政策建议。<br />
　　值得注意的是，此前中核集团宣布，2008年，中核集团实现主营业务收入366亿元,同<br />
比增长39%，增长速度处于全国大型工业企业前列;实现利润总额48亿元,比上年增长85%。<br />
在今年1~5月份，中核集团销售收入达到131个亿，比去年同期增长了12%，实现利润23亿,<br />
比去年同期增长了40%。<br />
