86岁囚犯服刑半个世纪跟不上时代不愿出狱(图)<br />
<br />
<img src="http://img1.gtimg.com/news/pics/28109/28109096.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
出狱那天，孙来有在被送往漯河市敬老院路上，哭嚎了一路，他说自己又高兴又难过<br />
<img src="http://img1.gtimg.com/news/pics/28109/28109076.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
生病或体弱的老年犯人一般被安排睡在下铺，以便照顾<br />
<img src="http://img1.gtimg.com/news/pics/28109/28109074.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
老年犯人监区里设有专门的医疗室，医生是名犯人，入狱前是某医院的专家<br />
<br />
2009 年10月24日，出狱那天，孙来有歪斜在轮椅上，哆嗦得像片树叶，河南省第三监狱的狱警们捏着他的手指，在释放证上按下红色手印。按规矩，犯人出狱前得留头发、洗个澡、换下囚衣，以迎接新生活，而这名86岁的老囚犯憋出浑身气力，双手拽住袖口，阻止狱警为他换上新衣服。<br />
<br />
很难想象，这个老人，偷过钱、扒过车，还强奸过未成年少女，半辈子五进五出监狱。方寸之地，孙来有度过了人生43年的光阴，终于在耄耋之年迎来了自由的日子。<br />
<br />
然而他哭闹着不愿离开。没有人接他回家，准确地说，他无家可归。第三次入狱后，他的家人便与他断绝了关系，在河南省第三监狱的十三年里，没人来看望过他。<br />
<br />
监区副监区长赵海伟根据他提供的家庭电话一个个地打，要么是空号，要么立刻挂断。在孙来有的卷宗里，惟一能找到的亲属笔录来自他的堂妹，“你们枪毙他算了，其他的也没啥要说的了。”<br />
<br />
每次出狱，都无法适应时代<br />
<br />
没人能完整叙述孙来有的过去，他自己讲话也已是含糊不清。<br />
<br />
直到出狱前三个月，监狱方按例行联系孙来有的家人，翻阅卷宗，这位86岁老人的过往才被零碎地拼凑出来。<br />
<br />
1923年，孙来有生于河南省漯河市，解放前参加过国民党青年军，官至排长。解放后，在漯河铁路局做行李房工人，1954年因偷了30块大洋被判入狱一年。档案显示，在押期间，孙来有因抗拒改造，被加刑两年。之后的三次犯罪，间隔都不超过三年，都是因为盗窃。<br />
<br />
孙来有说，自己这辈子，一是被国民党害了，二是被30块大洋害了。第一次刑满释放，他还有家可回，第二次就找不到妻女了。他一度被堂妹收留，帮着卖猪肉，后来不知因何争吵被赶了出来，那是他最后一次见到家人。<br />
<br />
1957年，第一次出狱时，孙来有已经快40岁了，没技术、没土地、没户籍、没“单位”。之后的每一次出狱，他都面对着一个剧变之后、他无法适应的新时代。<br />
<br />
孙来有曾经想找事做，理论上，他也应该获得帮助。犯人刑满释放时，如与家人联系不上，监狱必须将其送到原户籍所在地民政局帮教办。但每一次，孙来有都被告知自个儿离开，自寻出路。而每一次，孙来有又会和号子里认识的朋友走到一起，他们教他怎么分工合作偷东西。但他在这方面的天分显然不高，被抓的缘由要么是偷抽油烟机，要么是偷百货用品。<br />
<br />
惟一把“警官”喊成“政府”的犯人<br />
<br />
1988 年“严打”期间，孙来有偷了一包百货用品，被判入狱八年。1996年，第四次刑满释放的孙来有在漯河农村找到一份看瓜棚的活儿，对方不付工钱，只管吃住。在那个瓜棚里他强奸了一位未成年少女，愤怒的村民将他扭送到派出所。法院以奸淫幼女罪判其有期徒刑17年，那一年，他已经73岁。<br />
<br />
孙来有在河南省第三监狱的档案显示，在这里的十三年里，他从来没犯过错，打扫厕所、照顾病号，小心谨慎得像个小学生。<br />
<br />
监房里住16人，年老者统一安排睡下铺，每间房配备有独立的卫生间。这是孙来有住过最好的地方，狱外不多的自由时光，他睡过天桥、进过收容所，还住过瓜棚。<br />
<br />
孙来有和他的狱友们不必出操、上教育课和到厂房做工。平时，他们就坐在宿舍里，听识字的狱友逐字逐句念狱报，内容都是些改造心得与歌颂党和国家的诗文。孙来有听着听着会经常睡着。惟一让他提神的活动是电视戏曲节目和监区组织的戏迷乐园，每逢有节目，他都会搬上小板凳，坐在前排，带头叫好。<br />
<br />
记诵监狱规章也是老年犯每天的例行“作业”，记忆力不错的孙来有对此十分投入，并乐于在狱友面前展示“才艺”。这也是他最受尊重的时刻。<br />
<br />
初中毕业的他已经对外部世界失去了感知，他不知道什么是“奥运”。他是监区里惟一一个把“警官”喊成“政府”的犯人。有狱警批评他，“几十年前的老叫法，还总改不过来，以后出去了你怎么适应?”孙来有立刻回答，“是，政府!”<br />
<br />
“政府，我想回监狱”<br />
<br />
时光已然流逝，出狱的日期越来越近。监狱警官赵海伟每次将联系家人的最新进展告诉孙来有，都会引起他长时间的哭嚎。<br />
<br />
此时，86岁的孙来有因年老体衰已瘫痪在床近三年。为了他的生活起居，监区给他配备了两名护理人员，负责他的服药、洗澡、洗衣和吃饭。饭食为监狱特供，包括一盒牛奶、一个鸡蛋或蛋花汤。监狱倾力保证老年犯思想的稳定及身体的健康，以防有人自杀或死亡。<br />
<br />
监狱要求护理人员尽心尽力、照顾周到。护理人员的一举一动都在监区24小时监视屏上，这与他们的改造表现直接相关。腿脚还灵光时，孙来有干过这个差事。<br />
<br />
在赵海伟记忆中，孙来有绝非首个不愿出狱的犯人，此前还有好几个老囚犯，回到家中受到子女的照顾远不如监狱，天天守在监狱大门，哭喊着要求回“家”。<br />
<br />
孙来有也越来越不愿离开“家”，出狱前半年，他收到减刑裁定书时，甚至发了通脾气。<br />
<br />
然而，无论如何，他必须按时出狱，“监狱毕竟不是福利院。”赵海伟说。2008年，司法部要求全国各监狱，将刑满释放人员交到亲人、村委会(居委会)、司法部门等手中，以降低重新犯罪率，稳定社会治安。此前，河南三监及全国其他监狱也启动过无缝对接，主要是上门说服心灰意冷的家人，及与当地司法、民政部门协调安置好刑满释放者。<br />
<br />
对河南三监而言，这么多年，超过80岁且无住所、无亲人、无户籍、送不出去的人迄今只有孙来有一例。所有被监狱找上门的单位都拒绝接受孙来有，他们有他们的道理，在福利资源尚稀缺的情况下，为什么要将昂贵的医疗费和低保名额用在一个刑满释放人员身上?<br />
<br />
最终，在一位直接能拍板的领导的支持下，漯河市敬老院答应接收孙来有，但希望低调进行，因为街头还有其他无家可归的老人，“如果他们上门质问凭什么能收留他，不收留我，我们无言以对”。<br />
<br />
被强行送到敬老院后最初的几天，孙来有脾气很大，常常抱怨护工没有全天24小时伺候他，要求换人，否则向“政府”告状。过了一星期，他才逐渐意识到，这儿的护工照顾他，并不是为了加分减刑，有时被惹恼了，他们会训斥他，这个“不知感恩的老混蛋”。<br />
<br />
一个月后，赵海伟到漯河敬老院回访，蜷缩在床的一角，像一团揉皱了的纸的孙来有，立即展开双手，伸向赵，“政府，我想回监狱”。<br />
<br />
赵海伟也感到困惑，监狱需要执行一项特殊的工作――将不愿出狱的人劝出监狱。这些天，他们又在忙着为一个73岁不愿离开的刑满释放者张罗出狱后的安身之地。赵海伟说，路过第三监狱因乌鲁木齐越狱事件而加固的围墙时，这个老囚犯嘴里不住喃喃，为什么会有人越狱……<br />
<br />
2009年12月底，孙来有出狱两个月后，病逝于漯河敬老院。<br />
