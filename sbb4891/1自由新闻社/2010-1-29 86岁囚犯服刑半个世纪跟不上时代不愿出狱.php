老年犯们。孙来由是他们中第一个无单位接收的老年犯。他们有他们的道理，在福利资源尚稀缺的情况下，为什么要将昂贵的医疗费和低保名额用在一个刑满释放人员身上?在监狱系统，老年犯的关押与改造问题一直备受关注，这个群体易被家人抛弃，易出现心理问题，自杀率高;他们出狱后的去处与出路，常常困扰着监狱警察。<br />
