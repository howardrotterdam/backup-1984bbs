“没有张文彬，就不会有文强” 到底是谁提拔了文强 <br />
 <br />
发布日期：2009-10-21 15:02:43　　　新闻来源：《环球财经》杂志　　　编辑：陈新 <br />
&nbsp;&nbsp;<br />
　　当重庆市司法局局长文强被“双规”之后，原全国人大代表、82岁的重庆市人民政府参事雷亨顺不无感叹地说，“文强的问题历届政协委员和人大代表反映很多，为什么动不了？”<br />
<br />
　　并非没人动文强，这十几年文强的职务“动”了好几次：从副处级的县委副书记，一路“动迁”升到了正厅级的直辖市常务副局长。<br />
<br />
　　“贵人”书记<br />
<br />
　　“有的同志问我，文强是你选上来的，他平时来不来你这边？我说，不来”。已经退休的原重庆市政协主任张文彬，并不否认其对文强的提拔。 当地官场上则流传着一句话“没有张文彬，就不会有文强”。<br />
<br />
　　可以基本确定，两人相识于重庆巴县。<br />
<br />
　　官方资料显示：张文彬出于生1934年，于1978年任巴县县委副书记，1981年3月任县委书记。在此期间，比他小20岁的文强一直任职于巴县政法系统，并在这里完成了从普通科员到副处级的跨越：1972年参加工作，分配到巴县(今巴南区)公安局秘书股，1983年，成为县委政法口的常委，1985年，升任分管政法的县委副书记。<br />
<br />
　　文强升任副书记的次年，张文彬离开了巴县，担任重庆市政府财贸办主任。 5年后，文强也获得了进入重庆的机会。<br />
<br />
　　这一年，36岁的文强被市委组织部推荐给了市公安局。也正是在这一年，张文彬升任重庆市委副书记兼政法委书记。<br />
<br />
　　“从中国的人事体制来看，对公安局副局级以下重要官员的任命，政法委书记往往起到决定性作用”，国家行政学院的一位专家指出。<br />
<br />
　　天时 地利<br />
<br />
　　除了上层的“人和”和当时恰逢中央提倡“干部年轻化”的天时，对于文强的这次提拔，巴县的“地利”也不可忽视。<br />
<br />
　　重庆官员流动有个重要传统，即主城区的官员大多从周围区县选拔。而巴县是距离主城区最近、经济实力最大的一个县。因此，巴县的官员上调，一般都出任重要岗位。<br />
<br />
　　据知情人士透露，当时市公安局本来是想调巴南区的人大主任，曾在市公安局工作过的刘圣福。但文强得到了包括张文彬和刘圣福在内的一致举荐，主要理由是觉得他“吃得苦，聪明，肯干”。当时老县委书记已经去世了一段时间，文强作为县委副书记，实际担负着一把手的重任。<br />
<br />
　　当时有两位巴县干部反映文强素质不高、工作作风有问题。据当地一位记者回忆，一位前市委常委去巴县视察工作，酒酣耳热之后，文强和平时关系不错的刘圣福吵起来，让这位常委觉得很不快。有一次，文强甚至在酒席上和一位与自己关系不和的巴县领导抡起酒瓶。不过，张文彬却表示，文强在巴县“很规矩的，到了市里以后，说话才开始不拘小节，不时口带脏话”。<br />
<br />
　　1992年，文强出任市公安局副局长，分管刑侦。知情人士解释了最终任用文强的考虑：他年轻，才37岁。市局当时还有一批解放后就参加工作的老领导，在警察队伍中有威望，应该可以约束他。另一个很重要的原因是，市委组织部已经做出了决定，公安局并没有太多的发言权。<br />
<br />
　　谦恭未“篡”时<br />
<br />
　　刚到一个更高层级的权力系统，文强显得很恭敬，他称呼局里的老领导为“老人家”，并表现出了自己在刑侦方面的才华。时任局长的王文德搞敌情出身，懂侦查业务，他也认可文强“肯钻研，分析案情能说到点子上”。刚到重庆两年，文强领导的刑侦队伍破获了两起全国闻名的大案：1992年重庆警匪枪战，1994年中国第一盗案。业内人士透露，上世纪90年代，重庆的刑侦力量在全国排前几位，丝毫不逊色于广东等大城市。<br />
<br />
　　但文强酒后滋事的事情又再次发生。除了嗜酒，文强还表现出和黑社会打成一片的江湖气。王平、王渝南被认为是解放后重庆第一代黑社会的代表。文强和王平交往甚密，坊间广为传播的一个事例是：他高调参加了王平女儿的生日宴会。据业内人士介绍：“搞刑侦的人和黑社会交往的情况有两类，一是从工作出发，同流但不合污。另一类是从工作出发，慢慢以无原则对原则，然后将这种交往变为牟利的工具。文强属于后者。”<br />
<br />
　　仕途搁浅<br />
<br />
　　1997年，重庆直辖。文强面临着进重庆后第一次重要晋升机会。市局的老领导退休，市长王鸿举正在考虑新的常务副局长人选，文强是最有力的竞争者：他在公安局的资历比较深，还拥有相当于硕士的学历(文强毕业于泸州警察学校，但在巴县任职时，曾在西南师范大学研究生进修班在职学习两年，这让他拥有了在重庆同级公安官员中的最高学历)。<br />
<br />
　　但文强张扬的性格没有在系统内部获得支持。业内人士称，“常务副局长应该让没有野心的人来做，和一把手之间才能搭配好。”最受非议的是张君被抓获后，文强在电视上频频露面，讲自己在破案中的作为，在公安系统内引起很多不满，其实，张君被抓获的一个重要起因是湖南公安先发现线索，提供给重庆。最后公安部嘉奖的时候，文强被记一等功，湖南方面却颗粒无收。后来重庆市局一位老领导去湖南开会，湖南方面的人当面表示不满，称文强“是政治流氓”。<br />
<br />
　　因此，当市长王鸿举向一些老同志征求常务副局长的人选意见，分管治安的副局长陈焕奎获得了更多的支持，并最后出任常务副局长。<br />
<br />
　　从巴南进入重庆市公安局做副局长后的10年时间，文强的仕途搁浅了。虽然已经在公安局挑大梁，进入时间也最久，但一直再未获得行政职务上的提升。<br />
<br />
　　仕途停滞时，文强却从另一个渠道找到了能量的提升空间。<br />
<br />
　　黑白空间<br />
<br />
　　直辖为重庆带来了大量建设资金。2007年全国“两会”前夕，重庆市长王鸿举向中外媒体形容这个速变的城市：每3个月就要更新一版地图。但随着经济的发展，重庆的黑社会也“进化”出了与上一代黑社会不同的特点：“大多都拥有实体，看起来都是正行，但是用非法的手段经营。”<br />
<br />
　　1997年后也是文强在黑白两道的快速成长期。对于他在此位置上坐大概有两种解释，一是重庆市公安局的官员任命传统，由政法委书记兼任公安局长，因此只有一半的精力能放在公安局的管理上；二是公安局长多是由外系统调入，本身不懂业务，在专业性极强的刑侦工作上，不得不倚靠文强。这些都为文强形成自己的权威提供了时机。<br />
<br />
　　一次办案中，两名犯罪嫌疑人持枪在一座小楼中负隅顽抗，公安干警久拖不能拿下，文强来到现场，拿起扩音器喊话：“我是文强，限你们3分钟内投降。”3分钟后，两名嫌疑人无条件投降。在谈论这件事时，圈内人认为这更多体现的是文强在黑道中的权威。<br />
<br />
　　在特定环境下，文强的这种“权威”似乎不完全是坏事。而即使是对文强为人持负面评价的人士，也肯定他对本职工作“敬业”。抓到张君后一脚踹到张君的脸上，这曾被解读为他爱作秀的证据，但一位文强的老领导认为，这种兴奋是真实的，“是他的个性”。可供佐证的数据是，重庆这些年的刑侦破案率介于85%到89%之间，略低于全国91%的平均水平，“但考虑到重庆的复杂情况，他的本职工作未必出类拔萃，却也是能完成的”。<br />
<br />
　　2001年是文强人生的高峰。“共和国第一刑事大案” 张君案在重庆的破获，不仅让文强摆脱了因王平事件的危机，还成为他仕途的推动力。2001年，文强升为一级警监，正厅级侦察员，此后不久，升任常务副局长，成为二把手，搞科技出身、曾被评价为“太老实”的王华刚成为三把手。据说，新任的公安局局长朱明国曾就这个人选安排咨询原来的老领导，得到的回答是：“用文强的才干，用王华刚的品行。”<br />
<br />
　　2009年8月，文强因涉嫌黑社会保护伞、强奸等被双规。在这一年前，他被调离他经营了数十年的公安系统，调任重庆市司法局长，这是他最后一次，或许也是唯一一次没有“贵人”相助的“升迁”。</div>

							
							
							
							
