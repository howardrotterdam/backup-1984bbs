恕我直言和一家之见，我认为此信并非出自钓头之手，这封信逻辑强大，言简意赅，甚至前面一段身世简介还带有一定文学性，主题潜伏极深，属于从上往下式的控制，也就是说，信的作者一直是在以控制的笔法写这封信，断句比较短，逗号比较多，说明作者想靠打散长句来竭力掩饰自己平时写作的风格。<br />
钓头这样的职业群，本来就有很大的流动性，这件事情干不了了，一般来说就去想别的出路了，因为大势已去，他们对那些车主们也都没有什么普遍爱恨，对于这个职业也没有什么留恋，不会傻等着去重操旧业的。这样文绉绉的写一封信，当下并不会取得任何的效果，对于普遍文化水平比较低比较看重眼前利益的钓头或者钓鱼群体来说，是等不及去寄信或者等待其发挥其长远效果的。而且信的逻辑是，威胁你的家人，除非你不向政府追究这件事情了。但这是私人的A和官方的B的事情，他一个连合同工都轮不上的C是没有必要进行威胁的。他们也没有必要反复提及律师姓名和用比较大的篇幅去刻意的贬低，我相信，对于这群人来说，是哪个律师在打官司他们都未必知道，就算知道，也不会去想那么多。文中对于郝律师的贬低太过于刻意，语气略带台词感，在阅读的时候有比较大的脱离。这点让我觉得有点莫名。<br />
 <br />
所有的怀疑都将再一次集中到了官方，但是这次，我觉得官方也不会做类似的事情的。首先，官方其实不如大家想象的那么敏感，往往我们觉得天大的事情，很多官方部门都不知道。官方对于已经过去的事情的处理方式是大事化小，小事化了。因为这件事情面对的是官方的几个大的部门，部门中包括当地政府，运管局等相关部门直到法院，这些都是大的单位。而写信是个人的行为。如果不是和自己的切身利益挂钩，而仅仅是单位的名誉或者利益受损，没有哪个在其中供职的公务员会这么多管闲事的。公务员遇事一般都采取推脱的态度，这样富有激情往上冲的并非公务员行为。公务员一般来讲并不那么热爱自己的工作，只是热爱自己的待遇，冒着被查出违法的危险，不沾亲带故，他何必呢。<br />
 <br />
于是，还有可能就是更上一层政府行为。但是这点说实话我也是不相信的，因为政府深知这次栽在了舆论上，尤其是网络舆论，这样的一封信，其结果势必就是再被放在网络上，他们再笨也不会这么笨，他们再无聊也不会这么无聊，这是莫名其妙给自己找事。而且如果是政府行为，那么这件事情就势必不止一个人知道，没有任何一个机构愿意冒这个莫名其妙的风险的。尤其是在没有利益和目的驱动的情况，因为对于政府来说，这件事情已经过去了，从舆论处理的角度来讲也已经冷却了，这是政府最愿意看见的情况。政府处理类似事情的方式一般是找到当事人和当事人亲人的单位的领导，让其单位给其做思想工作并进行软威胁。写一封威胁信是不会的，总得有人执行，你让哪个部门出面？<br />
 <br />
另外的疑点有1：如果有人能把另外一个人的家庭查的那么清楚进行威胁，那么他不可能对自己介绍的那么详细。<br />
疑点2：如果是其他地方的人写信，想把矛头引向钓头，那么他不会给钓头加那么多深情表白。<br />
疑点3：如果我收到了这封信，我的第一反应就是对家人的担忧，第二反应就是报警请求查证和对家人的保护，而不是率先公开此信寻求舆论支持并对这些人进行大胆的指责，除非我知道，家人其实没有危险。<br />
 <br />
&nbsp; &nbsp; 那么这份信是谁写的呢？他可能是最不愿意让这件事情过去的人。他可能真的是个有文化的钓头。他可能对于使用舆论有一定的心得和野心的人。他可能是可以通过这样一封信获益最多的人。他也可能是个看热闹的人。他可能是一个想再推动一下此事的热血公民。可能是一个受了气的政府工作文员。他可能是法官。他也可能是律师。他可能是原告。他可能是被告。太刺激了，他到底是谁啊。欢迎大家参与破案。警方不是查言论那么利索么，网络上发个言都能分分钟追到人家家里，邮筒里塞一封信还不赶紧去查查。大家都是好人，出于好心，希望所有的好人都站在光明磊落的地方，否则和他们有何区别呢。无论如何，希望张军和郝律师可以将官司打到底，虽然在我国，法院是政府的一个下属部门，但他们都应该为此付出更大的代价。</div>

							
							
							
							
