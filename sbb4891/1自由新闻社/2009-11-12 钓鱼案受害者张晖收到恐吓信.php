													<h2>李承鹏：一封信，好惊悚</h2>
						
						
																											<div id="postmessage_325734" class="t_msgfont"><a href="http://blog.sina.com.cn/s/blog_46e7ba410100fvbh.html" target="_blank">http://blog.sina.com.cn/s/blog_46e7ba410100fvbh.html</a><br />
<br />
夜深深，来了MSN。韩寒在MSN里给我一个链接，《一封信》，这封信，问我是否同意他的分析。<br />
<br />
一开始我觉得他多虑了，钓头有点文化是可以的，捕头、蛇头以及龙头也可以，而且汉语写作是有偶然性的，更依仗的是对方块字的解构和句意组合，不像西语还有阴阳格和时态，所以农民兄弟的信天游也可弄得技惊四座；更何况那些执法队这次栽了场子，心中忿忿，他们现在又不能耍过去擅长的办法，就装低调装煽情，这如同打架，退到墙根后其实防守面积小，进攻更富针对性。<br />
<br />
还有就是有关部门的人不会干这种无聊的事，我倒觉得，很多时候他们做事并没有仔细计算效率、结果、目标，他只是想这么做，这么做他就爽，否则我们也不会看到马路修了又挖，挖了又修，或者脱口而出你究竟代表党，还是代表老百姓。通常他们都不是大奸臣，只是小笨蛋。所以这也可能是执法队长的队副、马仔见老大郁郁寡欢，马仔就找来马仔的马仔写信一封，对张军晓之以情，动之以理。而写匿名信威胁又是这个国家几千年文化的必然产物，大家摸起来轻车熟路，兴之所致，信之所致。<br />
<br />
可后来我发现其实是我多虑了，表面看这封信是一个罗生门，钓头、执法队、法院、律师、记者、路人甲甚至张军本人都有可能，但中间还是有概率大小，钓头、执法队、法院写信就是小概率事件，因为他们现在是希望事情早点过去，钓头因为所处生存链条较末端想打击报复，率先出手是可能的，但现在的情况是钓头其实前所未有的被官员控制着，因为官员不想再惹事，打击报复即使不等十年也得等十个月。执法队内心也是想写这封信的，可一是他们没那么文艺，二是这次伤亡实在惨重，要是再被媒体抓住了，浆糊捣来捣去就很不好看。法院也是同理。<br />
<br />
总之一句话，损人不利己的事情其实是越来越少，让钓头以炮灰的可怜状写这封信就是损人不利己，不仅达不到预期而且炮灰可能成为引线，形成下一轮网络攻势。记者和路人甲的概率就更小，因为这太绕了。<br />
<br />
让我们换个思路，其实写这封信的人，就是能从这封信得到最大好处的人，谁能得到最大好处？我碰巧看了一个郝劲松接受人物周刊的采访。人物周刊：可曾有过这样的感觉：你以为有人在背后支持你，推你，可是真的发生什么动真格的事情，你回头一看，身后没有人，你一个人在孤军奋战。郝劲松：迄今为止我还没有这种感受，每次我身后的人都巨多。我研究二战时候的一些战役，德军为了打断盟军，就开展“狼群战术”，在大西洋分布了上百艘潜艇，神出鬼没，击毁了大量盟军的运输船，盟军也采取了相应对策。这种“狼群战术”有时是非常管用的，网民就是我的狼群！<br />
<br />
看得出郝劲松是非常明白网民的力量的，这件事对他的好处是最大的，而这件事渐渐冷下去是郝劲松最不愿意看到的，毫不讳言郝律师是一个维权斗士，他是很想推动中国法律向前的一个积极份子，如果此事继续热下去，他的理想就可持续发展，所以他想制作出一封信来再次发动狼群。这样看很武断，我也不相信这是郝劲松干的，但如果是郝律师干的，就确实太刺激太有深意了，如果真是郝劲松，那就真是好惊悚。<br />
<br />
从这封信看得出钓头是在软威胁张军，也看得出钓头及其弟兄有铤而走险的可能。这封信公布出来其实对钓头方是十分不利的，而促使外界继续支持张军。所以，谁写的这封信，这个人一定是熟知网络传媒好处而且很有欲望有野心地利用舆论来达到某个目的，虽然他相达到的这个目的看上去并不坏。但我们更需要的是真相，而不是真理，我们需要正常，而不是正确。郝律师无疑是代表善的，可现在有一个问题是，我们喜欢用善与恶，来代表着法律，我们都有善恶观，但没有法律观。<br />
<br />
所以上海钓鱼执法不是简单的一个非法执法队面对一些弱势，荆州打捞也不止是一个黑心船老大面对三个大学生，它们是，但不止是，这个国家骨子里有些东西是很可怕的，说吃人也惊悚了一些，但几千年来肯定有两个字，骗人。中华人民币共和国，不仅是执法队和船老大，我们自己，每天，也在这样干。<br />
<br />
调查这封信的出处其实很简单，比如制作过程中肯定有指纹，在办理二代身份证时我们的指纹早已悄悄入库，如果指纹被抹去了，就更证明那不是钓头们干的，这么细腻就不是钓头而是捕头了；还比如询问张军，这事张军也可能知道一些风声，像《风声》那样依稀感觉得到谁可能是老鬼。<br />
<br />
所以未来的时间还会有一些行动的，以上仅是案情分析，不代表我的观点，我也不敢认为是郝劲松，如是郝劲松，那才好惊悚。这古怪的国家，和司法，和律师，和一切……<br />
<br />
以上，答韩寒。</div>

							
							
							
							
