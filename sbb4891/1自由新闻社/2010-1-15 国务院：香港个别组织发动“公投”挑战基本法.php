													<h2>评港澳办关於五区公投的谈话</h2>
						
						
																											<div id="postmessage_434347" class="t_msgfont">练乙铮, 信报主P, 2010年1月16日<br />
<br />
评港澳办关於五区公投的谈话 <br />
<br />
有关五区公投的辩论，愈来愈有意思。前一阶段，支持和反对双方，就「到底是不是公投」丶「什麽条件满足了才算是公投」，有激烈争议，吵得面红耳热，还是没有 共识。举例说，有人认为，如果当权派不应战，社丶公两党参选人在无对手情况下低票当选重返立法会，则补选意义大於公投，甚或公投意义等如零；如此等等，总之意见十分分歧。特区政府立场很清晰，一早申明：这不是公投，是补选。不过，昨天港澳办高分贝加入战圈，正式指摘「个别社会组织」推动五区公投运动，从根本上违背基本法。<strong>港澳办此举，无疑替悬而未决的「是否公投」争议一锤定音：是公投！</strong><br />
<br />
港澳办说：「公投是由宪制性法律加以规定的，是一种宪制性安排，具有特定的政治和法律含义；香港特别行政区基本法没有规定公投制度，亦无权创制公投制度。」按此，在香港搞公投，当然不符基本法。港澳办指摘社丶公两党，正好显示它认为这两个党搞的是公投，不然港澳办用不着反对，就算反对，用的也只能是其他罪名较轻的理由，如林瑞麟说的浪费公帑等。港澳办这个立场，刚好与特区政府及其本地支持者的看法相反，难免会让这些人感到尴尬，甚至当权派能否派人竞选，现在也成问题─ 怎可以参加违法活动呢？好在，港澳办的说法站不住脚，因为<strong>它混淆了「形式」和「实质」的分别。</strong><br />
<br />
社丶公两党这次搞的「所谓公投」，<strong>形式上当然不是公投，只不过是一次五区补选，</strong>港人不同观点之间的争拗，是想搞清楚社丶公两党的行动，是否具备公投的一部分实质，亦即能否从补选结果中清晰得出「市民要求尽快双普选丶取消功能组别」这个政治结论。然而，就算补选的结果清楚表达这个诉求，<strong>其功效也只不过是道义上的，顶多只构成对特区政府及中央的一个舆论压力，却并不具法律效用，这点香港人完全清楚，实在用不着港澳办发言人强调。而且，社丶公两党的动作，因为形式上不是公投，并没有违反基本法，故港澳办的「违背基本法」指控，完全不成立。</strong><br />
<br />
事实上，<strong>在香港，有资格搞公投因而违反基本法的，理论上只有特区政府。</strong>人民团体便是打正旗号「搞公投」，无论搞得多彻底，也只能算是一种民意调查，谈不上违法，除非（一）法律连民意调查也不容许，或者（二）特区立法会通过具体立法，不准搞大於某一规模的抽样调查；但如果要立那样的法，便是天大的笑话─ 那有政府害怕民意到了那个地步的？而且，基本法可说是香港的宪法，宪法是用来规范政府行为的，「特区没有剩馀权力」，也仅指特区政府行为而言；政府却不可以把人民（个人或团体）告上法庭，说後者违反宪法或基本法。全世界没有任何一个国家的政府，包括中国政府在内，胆敢控告人民违宪─ 那样会闹笑话。政府只能通过具体立法，阐明宪法中的人民权利界限，人民如果违法，是违背这些具体法例；然而，香港的具体立法里头，没有说不可以作「形式上不是丶而实质上可能是公投」的政治行为，因而是可以的，具体法律不存在「剩馀权力」问题。<br />
<br />
此外，港澳办还指摘「一些人…公然挑战…全国人大常委会有关决定」，这也站不住脚。首先，五区公投的议题，已由社丶公两党定作「尽快实现真普选，废除功能组别」，<strong>因为没有指明年份，并不违反人大常委会的任何决定。其次，人民团体「挑战」宪法或立法组织的一些决定，在一个开放社会，完全是允许的，社丶公两党作出挑战，港澳办完全不应用「蓄意」，「公然」等充满敌意且暗指违法的字眼去描述这种合法行为。只有在极权社会丶党国有绝对权力的地方如中国大陆，这类字眼才勉强派得上用场，拿来香港用，大多数香港人反感，笔者亦有些反感。</strong><br />
<br />
最後，港澳办还说，「挑战基本法和人大常委会有关决定，只会引发争议，损害来之不易的良好（经济和社会稳定）局面」。这个说法亦嫌牵强。进行有关政制的争议，不见得会破坏良好社会局面。香港八丶九十年代的中英争拗，并没有影响香港经济，相反，那十多年是香港经济发展最旺盛的时期。反过来说，中国大陆在「一言堂」年代，社会经济发展都不是最好的。社会局面不稳定，有其他原因和议题导致，政制争议包括对基本法和人大常委会决定的挑战，就算涉及示威游行，也不过是纸上谈兵，并不影响经济。<strong>香港○三年那次五十万人大游行一过，市民出了一口鸟气，心情稍稍舒畅，经济反而马上回升了。</strong><br />
<br />
至於港澳办说：「关於香港政制的发展问题，无论大家的意见丶立场有多大的不同，我们都必须严格按照基本法所确定的程式办事，而任何偏离基本法所规定的程式的行为做法，都无助达成社会共识」，笔者倒认为是对的，但在五区公投事上，社丶公两党并无违反基本法或人大常委会决定，反而是港澳办发言人的指控，好几个地方过了火位，应该考虑收回并致歉意。<br />
