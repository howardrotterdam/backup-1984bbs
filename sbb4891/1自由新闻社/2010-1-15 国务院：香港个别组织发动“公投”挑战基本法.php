<br />
港澳办的声明指出，「公投」是由宪制性法律加以规定，是一种宪制性安排，具有特定的政治和法律含义。香港特区没有规定「公投」制度，香港特区是中国的一个地方行政区域，无权创制「公投」制度。在香港推行「公投」，没有宪制性法律依据，没有法律效力。<br />
<br />
声明说，《基本法》和人大常委会的有关决定，已对香港特区政治体制的未来发展，包括行政长官和立法会实行普选须遵循的原则和程序作出明确规定。在香港就政制发展问题进行「公投」，是从根本上违背《基本法》和人大常委会有关决定。<br />
<br />
中联办负责人同日接受新华社访问说，所谓「公投」，「不利于香港政治体制……循序渐进向前发展，不利于香港社会政治的稳定和经济的发展，有损广大香港市民的福祉」。<br />
