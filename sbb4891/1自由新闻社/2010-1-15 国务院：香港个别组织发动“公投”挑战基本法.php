<br />
近日，香港个别社会组织宣布将推行以“尽快实现真普选，废除功能组别”为主题的“五区公投运动”。对此，我们表示严重关注。<br />
<br />
众所周知，“公投”是由宪制性法律加以规定的，是一种宪制性安排，具有特定的政治和法律含义。香港特别行政区基本法没有规定“公投”制度。香港特别行政区是中华人民共和国的一个地方行政区域，无权创制“公投”制度。在香港特别行政区进行所谓“公投”没有宪制性法律依据，没有法律效力。香港特别行政区基本法和全国人大常委会所作的有关决定，已对香港特别行政区政治体制的未来发展，包括行政长官和立法会实行普选所必须遵循的原则和程序作出明确规定。在香港特别行政区以任何形式对未来政制发展问题进行所谓“公投”，与香港特别行政区的法律地位不符，是从根本上违背香港特别行政区基本法和全国人大常委会有关决定的。<br />
<br />
在中央政府的大力支持和香港社会各界人士的共同努力下，香港特别行政区有效应对国际金融危机的冲击，保持了经济和社会大局的稳定。一些人蓄意推动所谓“五区公投运动”，公然挑战基本法和全国人大常委会有关决定，只会引发争议，损害这种来之不易的良好局面，相信这是香港公众所不愿见到的。<br />
<br />
&nbsp; &nbsp; 我们注意到，香港特别行政区许多社会人士已对“五区公投运动”表示强烈反对。曾荫权行政长官1月14日下午也在香港特别行政区立法会答问会上表示：在香港进行任何形式的所谓“公投”，都是完全没有法律基础和法律效力的，特区政府是不予承认的。关于香港政制的发展问题，无论大家的意见、立场有多大的不同，我们都必须严格按照基本法所确定的程序办事，而任何偏离基本法所规定的程序的行为做法，都无助达成社会共识。曾荫权行政长官和香港社会人士的上述看法符合基本法和全国人大常委会的有关决定，我们对此表示赞赏。<br />
<br />
我们相信，香港特别行政区各界人士一定能够明辨是非，在香港特别行政区基本法和全国人大常委会所作决定的基础上，理性务实地讨论2012年行政长官和立法会两个产生办法的修改问题，推动香港特别行政区的政治体制循序渐进地向前发展。<br />
