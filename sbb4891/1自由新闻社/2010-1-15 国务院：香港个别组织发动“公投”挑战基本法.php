--------------------------------------------------------------------------------<br />
港澳办狠批「公投」违基本法 评论员：中央担心倘成气候难驾驭 <br />
【明报专讯】对于公民党、社民连两党推行以「尽快实现双普选、废除功能组别」为题的「5区公投运动」，国务院港澳办发言人昨日表示严重关注，指在香港以任何形式就政制问题进行「公投」都是「与香港的法律地位不符」，根本上违背《基本法》和全国人大常委会有关政制发展的规定，一些人蓄意推动所谓「5区公投运动」，是公然挑战《基本法》及人大常委会决定。<br />
<br />
中央电视台昨晚《新闻联播》亦读出港澳办声明全文。特区政府发言人表示，所谓「公投」只会对进一步推动香港政制向前发展，设置新障碍。<br />
<br />
全国人大常委范徐丽泰认为，港澳办的做法是担心市民被误导，澄清香港并无公投。基本法委员会港方委员谭惠珠表示，香港属于中国的特别行政区，非主权国家，没有公投的权利，香港政制不能由公民自决，需由中央决定。她表示，「公投」字眼会令市民有错觉香港是有主权的地方，并不适当。<br />
<br />
