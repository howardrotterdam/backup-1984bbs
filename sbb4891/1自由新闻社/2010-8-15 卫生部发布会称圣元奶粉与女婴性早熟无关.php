													<h2>卫生部：湖北婴幼儿性早熟与食用圣元乳粉无关</h2>
						
						
																											<div id="postmessage_784098" class="t_msgfont">针对媒体报道有婴幼儿因食用圣元乳粉导致性早熟的情况，卫生部委托北京市疾病预防控制中心、中国检验检疫科学院等检测机构，采用国际通行的检测方法（《动物源食品中激素多残留检测方法 液相色谱-质谱-质谱法》GB/T21981-2008），对乳粉中雌激素和孕激素含量进行了平行检测。圣元乳粉样品来自湖北省患儿家中剩余乳粉，以及湖北武汉和北京市场销售的优博、优聪样品，共计42份样品。还抽取了国内外其他14家企业的20个品牌产品，共31份样品。<br />
<br />
检测结果表明，42份圣元乳粉中未检出己烯雌酚和醋酸甲孕酮等禁用的外源性性激素，内源性雌激素（17β-雌二醇和雌酮）和内源性孕激素（孕酮和17α-羟孕酮）的检出值分别为0.2-2.3μg/kg和13-72μg /kg，其中患儿家中存留样品雌激素和孕激素检出值分别为0.5μg/kg和33μg/kg。检测结果符合国内外文献报道的含量范围。<br />
<br />
湖北省对3例疑因食用乳粉致性早熟的病例经再次会诊和回访，并检测激素水平、骨龄等项目，未发现患儿生长发育明显加速，仅能判断为单纯性乳房早发育，为临床常见病例。此外，卫生部向部分国内儿童专科医疗机构调取的就诊资料显示，近年临床就诊的儿童性早熟病例数未见异常升高趋势，就诊的假性性早熟儿童中，纯母乳喂养和人工喂养的比例基本相当。<br />
<br />
综合以上检测结果和临床会诊意见,卫生部专家组评估认为，湖北3例婴幼儿单纯性乳房早发育与食用圣元优博婴幼儿乳粉没有关联,目前市场上抽检的圣元乳粉和其他婴幼儿乳粉激素含量没有异常。<br />
<br />
