专家：患儿雌激素在成人水平因其在微小青春期<br />
<br />
新华社记者：我想问一个问题，此前媒体报道湖北有专家说患儿检测的雌激素在成人的水平，这个应该怎么看，婴幼儿的雌激素应该在什么样的水平？谢谢。<br />
<br />
北京协和医院内分泌垂体性腺学组组长伍学焱教授: 一般说来，胎儿在母体中的雌激素水平是非常高的，当脐带剪断的时候，婴儿和母体之间的联系就没有了，但是很快婴儿体内就会建立起下丘脑――垂体――性腺的轴系，这是调节体内内分泌的重要的轴系之一。当出生的时候，这个轴系虽然还不太稳定，但是相对来说还是比较完备的。这个时期，体内的这些激素就会升高，我们叫微小青春期。为什么叫微小青春期呢？就跟我们青春期发育相似，垂性腺激素以及男孩子的雄性激素以及女孩子的雌性激素都可以有一个明显的升高，男孩子的雄激素可以到正常成年男性的低限值或者一半左右。有一些男孩子会出现阴茎的勃起，睾丸的增大，甚至出现一过性的粉刺，当然也有个体的差异。<br />
<br />
女孩子在两岁之前轴系是不稳定的，可以间断性地分泌雌激素，水平可以到50pg/毫升，这相当于什么水平呢？相当于正常成年女性的雌激素的低限值，这就是雌二醇的水平。对雌激素比较敏感的孩子，就会出现比较明显的乳房发育，我们说比较明显的就是从外面可以看到或者摸到的现象。微小青春期男性是在半岁之前，女性大部分在一岁之前，有极小部分到两岁左右。由于程度比较轻，持续的时间比较短，而且又跟以后大青春期很相似，所以我们把它叫微小青春期。</div>

							
							
							
							
