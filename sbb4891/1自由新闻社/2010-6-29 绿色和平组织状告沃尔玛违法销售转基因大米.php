反转基因玉米人士主要理由是不可知论，但是科学不是混沌不清的东西。诸如“能毒死害虫为什么不能毒死人类”之类的言论事实上都是十分可笑的，稍微做一下 research 就可以明白对人类无害的原因。下面引用一下 bt 抗虫毒素的原理解释：<div class="quote"><h5>引用:</h5><blockquote>在鳞翅目昆虫（蝴蝶，蛾子）的幼虫肠壁上，目前已发现的受体主要有4种2大类。第1大类是蛋白，主要有氨基肽酶N（Aminopeptidase N，简称APN），钙粘蛋白（Cadherin）和碱性磷酸酶（Alkaline Phosphatase）。第2大类是糖脂(glycolipids)。转基因作物中的Bt一般是表达成分子量为120KDa的原毒素（pro-toxin）而被昆虫摄入的。这个原毒素是没有杀虫效力的。在昆虫碱性的肠道环境中以及特定的蛋白酶作用下，原毒素会被切割成一个大约60-65KDa的具有杀虫效力的蛋白。这个蛋白是由3个功能域（domain）构成的。从氨基端到羧基端依次被称为Domain I，Domain II 和 Domain III。在这3个功能域中，Domain II和III是和肠壁受体结合的关键区域。只有和特定受体有效地结合后，Bt才被能锚碇在昆虫的肠壁上。现在被大家普遍接受的作用机制是：Bt的单体和第一受体氨基肽酶N结合后，被某种蛋白酶做了进一步的切割修饰，然后形成四聚体再与第二受体钙粘蛋白相结合。与钙粘蛋白结合后，Bt的空间构象会发生变化，其结果是Domain I插入肠壁细胞膜内，造成非特异性穿孔，进而导致肠壁细胞破裂，并最终致使昆虫死亡。简单概括地说，Bt杀虫的主要过程就是：摄入=〉激活=〉锚碇=〉穿孔=〉致死。其间任何一个环节出了问题，Bt就会丧失杀虫效力。人的胃是酸性环境，而且消化道内也不存在能与Bt结合的受体，所以Bt对人及其他非靶标生物体是非常安全的。</blockquote></div>需要指出的是，一般玉米主要用于各种加工食品原料以及饲料，甜玉米用于人类直接食用。有反转基因人士声称美国转基因玉米只有 12% 被人类食用就是因为这个原因，这个数据并不能证明美国限制转基因玉米。<br />
