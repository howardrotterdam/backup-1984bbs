													<h2>全球转基因食品健康损害事件一览</h2>
						
						
																											<div id="postmessage_712757" class="t_msgfont">1997德国农民克劳纳开始种植先正达Bt－176玉米试验田，头三年，玉米长势喜人、毫无虫害，当2001年，他将这种玉米用来喂养母牛时，牛开始剧烈腹泻并停止产奶，最后，他总共损失了70头牛。<br />
<br />
&nbsp; &nbsp; 1998年秋，苏格兰Rowett研究所的普兹泰教授（Pusztai）就在电视上公开宣称，他的实验证明，实验鼠肾脏、胸腺和脾脏生长异常或萎缩或生长不当，脑部萎缩，多个重要器官也遭到破坏，免疫系统变弱。<br />
<br />
&nbsp; &nbsp; 1999年，美国康耐尔大学的研究者John Losey在英国《自然》杂志上发表报告，用涂有转Bt基因玉米花粉的叶片喂养斑蝶，导致44%的幼虫死亡。　　<br />
<br />
&nbsp; &nbsp; 1997-1998年，英国等实验分析发现转基因食品导致某些动物健康异常和种植区域出现异常。英国政府资助的研究显示，食用了转基因土豆的老鼠出现了肝脏癌症早期症状、睾丸发育不全、免疫系统和神经系统部分萎缩等异常现象。　　<br />
<br />
&nbsp; &nbsp; 1998年，欧盟国家通过法律，把转基因农产品作业严格限制在实验室环境或封闭区域之内。　<br />
<br />
&nbsp; &nbsp; 2004年先正达研发的转基因Bt-176玉米爆发丑闻，德国黑森州北部农民从1997年开始试种Bt-176玉米，并用作奶牛的补充饲料，2000年当农民开始提高该玉米在饲料中的比例后，所有的牛都死了。 <br />
&nbsp; &nbsp; 2004年瑞士联邦技术研究院踢球植物学研究所海尔比克教授发现，Bt-176中的用来毒杀欧洲玉米螟的Bt毒素，无法分解，最终毒死了奶牛。　　<br />
<br />
&nbsp; &nbsp; 2005年5月22日，英国《独立报》又披露了知名生物技术/公/司/“孟山都”的一份报告，以转基因食品喂养的老鼠出现器官变异和血液成份改变的现象。　<br />
<br />
&nbsp; &nbsp; 2005年11月16日，澳大利亚联邦科学与工业研究组织（CSIRO）发表的一篇研究报告显示，一项持续4个星期的实验表明，被喂食了转基因豌豆的小白鼠的肺部产生了炎症，小白鼠发生过敏反应，并对其他过敏原更加敏感，并据此叫停了历时10年、耗资300万美元的转基因项目。<br />
<br />
&nbsp; &nbsp; 2006年，俄罗斯科学院高级神经活动和神经生理研究所科学家伊琳娜?艾尔马科娃博士研究发现，食用转基因大豆食物的老鼠，其幼鼠一半以上在出生后头三个星期死亡，是没有食用转基因大豆老鼠死亡率的6倍。　　<br />
<br />
&nbsp; &nbsp; 2007年，在奥地利政府的资助下，泽特克教授及其研究小组对孟都山/公/司/研发的“转基因玉米NK603（抗除草剂）和转基因玉米MON810（Bt抗虫）的杂交品种”进行了实验。在经过长达20周的观察之后，发现转基因产品影响了小鼠的生殖能力。　　<br />
<br />
&nbsp; &nbsp; 2008年意大利的科学家做了一个长期实验。他们用抗草甘膦转基因大豆喂养雌性小鼠长达24个月，结果发现食用GM大豆的雌性小鼠肝脏出现异常。　　<br />
<br />
&nbsp; &nbsp; 2007年10月和11月，美国《纽约时报》等媒体报道，经过长期周密跟踪观察，发现有两种转基因玉米种植导致伤害蝴蝶生存，对生态环境安全的威胁程度已经超出可接受水平。为此，欧盟已经做出了初步决定、禁止该转基因玉米的种子销售使用。　　<br />
<br />
&nbsp; &nbsp; 2007年，法国科学家证实，孟山都/公/司/出产的一种转基因玉米对人体肝脏和肾脏具有毒性。<br />
<br />
&nbsp; &nbsp; 2008年，美国科学家也证实了长时间喂食转基因玉米，小白鼠的免疫系统会受到损害，该研究成果发表在同年《农业与食品化学》杂志上。　　<br />
<br />
&nbsp; &nbsp; 2009年12月22日，法国生物技术委员会最终宣布，转基因玉米&quot;弊大于利&quot;，这等于转基因作物种植在法国的永久废除。　　<br />
<br />
&nbsp; &nbsp; 2009年12月一期《生物科学国际期刊》上发表的研究结果表明，三种孟山都/公/司/的转基因玉米能让老鼠的肝脏、肾脏和其它器官受损。三种转基因玉米品种，一种设计能抗广谱除草剂（即所谓的Roundup-ready），另外两种含有细菌衍生蛋白质，具有杀虫剂特性。这项研究利用了孟山都自己的原始数据。<br />
<br />
&nbsp; &nbsp; 2004年7月28日，美国国家科学院完成了特别专题研究并发布研究报告，指明：转基因食品可导致难以预见的主基因（Host DNA）破坏，而用现有的审核和监测系统，美国各政府机构不能发现这些破坏。美国国家科学院列举了审核转基因食品产品的时候所没发现的异常：<br />
<br />
&nbsp; &nbsp; - 食用了转基因玉米等转基因食物的老鼠，出现血细胞和肝脏细胞异常、肝脏比没食用的更重；<br />
<br />
&nbsp; &nbsp; - 食用了转基因玉米的猪，在美国中西部农场出现假孕或不育；<br />
<br />
&nbsp; &nbsp; - 食用了转基因玉米饲料的母牛，在德国实验农场非正常死亡；<br />
<br />
&nbsp; &nbsp; - 使用转基因饲料的鸡的死亡率比使用自然饲料的死亡率高出两倍；<br />
<br />
&nbsp; &nbsp; - 英国市场出现转基因大豆食品后，居民的过敏症上升了50%，巴西出现同样状况；<br />
<br />
&nbsp; &nbsp; - 被长期认为“安全”的转基因玉米，其效果并非如推广者说的那么理想，例如，菲律宾食用者出现了皮肤、小肠和呼吸系统的异常反应；　　<br />
<br />
&nbsp; &nbsp; 1997-1998年，英国等实验分析发现转基因食品导致某些动物健康异常和种植区域出现异常。英国政府资助的研究显示，食用了转基因土豆的老鼠出现了肝脏癌症早期症状、睾丸发育不全、免疫系统和神经系统部分萎缩等异常现象。<br />
<br />
&nbsp; &nbsp; 2000年到2001年，转基因种植区域发现生态环境出现异常获得更多证实，譬如，转基因玉米品种本身尚未发现异常，但其周围野生生态环境出现异常，而转基因甜菜等品种的野外试验显示其本身和环境都发现异常。为此，美国、加拿大、英国和欧盟国家的政府农业部门紧急成立农业生态环境保护工作组，对转基因种植区开始全天候的严密监控措施。　　<br />
<br />
&nbsp; &nbsp; 在江苏省“棉花之乡”盐城大丰市调查发现：该省转基因棉推广进入第5个年头后，转基因棉除了产量下降外，其质量也出现了严重下降，表现尚不如普通棉。　<br />
<br />
&nbsp; &nbsp; 英国的权威科学杂志《自然》刊登了美国康乃尔大学教授约翰・罗西的一篇论文，论文中指出，蝴蝶幼虫等农田益虫吃了撒有某种转基因玉米花粉的菜叶后会发育不良，死亡率特别高。　　<br />
<br />
&nbsp; &nbsp; 阿凡迪斯/公/司/生产的一种“星联”转基因玉米，由于可能引起人体的过敏反应，美国环保局仅批准其用于动物饲料，禁止其用于食品生产。<br />
<br />
&nbsp; &nbsp; Mayeno,A.N.等（1994）报告，发生一种新的，不明原因的病症，主要表现为嗜酸性肌痛。临床表现有麻痹、神经问题、痛性肿胀、皮肤发痒、心脏出现问题，记忆缺乏、头痛、光敏、消瘦（Brenneman,D.E.等，1993；Love,L.A.等，1993）。后查明系日本一/公/司/生的基因化工程细菌产生的色氨酸所致。食用者在3个月后发病，导致37人死亡，1500人体部份麻痹，5000多人发生偶尔性无力。据测定，含量为0.1%便可杀死人体。　　<br />
<br />
&nbsp; &nbsp; 美国国家科学院发布长篇科研调查报告清楚指明：越来越多的观察和发现证明，转基因农作物的种植和使用对动物和生态环境有潜在的安全威胁；必须对转基因作物种植区域实行强制性隔离措施，对转基因食品实行更严格的控制和检测监测。　　<br />
<br />
&nbsp; &nbsp; 国际环保组织绿色和平也表示极大的担忧。转基因水稻可能通过花粉传播而污染传统的水稻品种，对于环境与生物多样性造成严重威胁。　　<br />
<br />
&nbsp; &nbsp; 世卫组织主张对发展转基因食品持谨慎态度，尤其要慎重引进国外没有经过严格检验的转基因产品。<br />
<br />
&nbsp; &nbsp; <br />
<br />
&nbsp; &nbsp; 附：蒋高明教授介绍道<br />
<br />
&nbsp; &nbsp; 转基因食品的安全性在国外早就争议不断，不止“转基因马铃薯事件”一个案例。<br />
<br />
&nbsp; &nbsp; 1998年的“转基因马铃薯事件”就让全球民众恐慌了一把--英国科学家阿派特・普斯陶伊教授在电视节目上宣称，用某种转基因的马铃薯喂养大鼠，会导致大鼠体重及器官重量严重减轻，免疫系统被损坏。<br />
<br />
&nbsp; &nbsp; 2007年，法国科学家证实，孟山都/公/司出产的一种转基因玉米对人体肝脏和肾脏具有毒性。2008年，美国科学家也证实了长时间喂食转基因玉米，小白鼠的免疫系统会受到损害，该研究成果发表在同年《农业与食品化学》杂志上。2009年12月22日，法国生物技术委员会最终宣布，转基因玉米“弊大于利”，这等于转基因作物种植在法国的永久废除。<br />
<br />
&nbsp; &nbsp; 转基因食品的安全性是一方面，而转基因作物对生态环境的破坏，即基因污染是另一个值得关注的问题。<br />
<br />
