													<h2>绿色和平组织状告沃尔玛违法销售转基因大米</h2>
						
						
																											<div id="postmessage_711585" class="t_msgfont">绿色和平组织状告沃尔玛违法销售转基因大米<br />
&nbsp;&nbsp;2010年06月29日 03:54&nbsp;&nbsp;21世纪经济报道<br />
　　王志灵<br />
<br />
　　6月28日上午9点，深圳市罗湖区人民法院，北京恩洋律师事务所律师黄琳琳手持一份“民事起诉书”在此排队等候。在起诉书中，绿色和平状告沃尔玛深国投百货有限公司违法销售转基因大米。<br />
<br />
　　“目前能否立案需要与法院沟通，不过案件关键在于以什么案由对沃尔玛进行起诉，以及诉讼请求在后期执行阶段的可操作性。根本目的是杜绝零售商出售转基因大米给消费者。”黄琳琳说。<br />
<br />
　　这是国际绿色和平组织第一次采取法律手段来阻止转基因大米流入商业渠道。此前5年，该组织一直在通过不同方式强烈反对转基因食品商业化。<br />
<br />
　　对抗转基因大米<br />
<br />
　　今年5-6月间，国际环保组织绿色和平在武汉、深圳、东莞和佛山四个城市的沃尔玛、华润万家、永旺(吉之岛)和百佳超市分别进行了散装大米的抽样，并委托具有资质的第三方实验室对这些样品进行转基因成分检测。<br />
<br />
　　最新检测结果显示，沃尔玛深国投百货有限公司武汉市徐东大街分店抽样的名为“国产香米”的样品呈转基因阳性。<br />
<br />
　　这是该组织第二次抽检到沃尔玛旗下店面有出售转基因大米。据绿色和平提供报告显示，今年3月份，该组织在中国8个城市的9家大型连锁超市的19个门店展开调查，从各门店生鲜散装产品(蔬菜、水果和大米)柜台随机购买了83份样品进行检测，其中在湖南长沙沃尔玛超市黄兴南路分店发现有转基因大米出售。<br />
<br />
　　“转基因食品一直备受争议。” 绿色和平食品与农业项目主任王伟康说。<br />
<br />
　　今年年初，农业部颁发了两种转基因抗虫水稻的生物安全证书：“华恢1号”和“Bt汕优63”，这是中国第一次颁发转基因主粮的生物安全证书。涉及转基因主粮的该政策引起了专业人士和民间组织的激烈争议。<br />
<br />
　　绿色和平认为，转基因水稻影响环境和生物多样性，可能会导致更多农药使用、严重的次生虫害问题等；且转基因在气候变化下可能会更脆弱；在此基础上，转基因食品长期安全性未知，其中婴儿可能会成为高危人群；此外，中国的转基因水稻涉及多项国外专利，一旦商业化，可能会遭遇让出我国粮食主权。<br />
<br />
　　在绿色和平公布有转基因大米进入流通市场后，农业部多次表态称“批准发放安全证书并不等于就允许商业化生产”，并在两会期间强调称，目前中国仍未允许转基因大米的商业化生产和进入市场流通。<br />
<br />
　　根据《农业转基因生物安全管理条例》、《中华人民共和国种子法》及《主要农作物品种审定办法》等法律法规的规定，2009年获得中国农业部生物安全证书的两种转基因抗虫水稻，还需通过品种审定，并获得种子生产许可证和种子经营许可证后，方可进入商业化生产。<br />
<br />
　　因此，王伟康认为，中国尚不允许转基因大米商业化生产，依据相关法律，目前在中国的转基因大米商业化生产和销售都属违法。显然，沃尔玛超市在分店出售转基因大米，这一行为本身就是违法的。<br />
<br />
　　起诉沃尔玛<br />
<br />
　　“作为全球最大的连锁零售商，沃尔玛完全了解转基因食品对人体和环境的长远影响。沃尔玛早在2005年便向英国的消费者做出不会出售任何转基因产品的承诺。然而，沃尔玛不仅对中国的消费者执行了双重标准，更对中国的法律采取了漠视态度。”王伟康说。<br />
<br />
　　绿色和平早在3月份抽查结果出来后，就立即将该问题通过各种方式反映给沃尔玛相关部门，也曾与沃尔玛的相关管理人员进行过沟通，但是并没有得到该企业的解释。<br />
<br />
　　沃尔玛公关部姜薇表示，该公司尚未知绿色和平起诉沃尔玛一事，“沃尔玛一直很关注转基因食品与安全，但由于此事事态复杂，需要几个部门讨论后才能得出明确的结论与说法。”姜薇说。<br />
<br />
　　转基因大米从科研室进入商业生产领域是一个意外，据上述绿色和平调研人员透露，从3月份开始，国家农业行政主管部门已经开始在查处此案。<br />
<br />
　　而零售商是直接向终端消费者提供商品的最重要环节，有职责保证其提供的产品是合法安全的，因此，零售商应当主动采取安全监控措施防止转基因大米流入到不知情的消费者手里。<br />
<br />
　　黄琳琳认为，绿色和平对沃尔玛的中国总部的起诉，可以以“买卖合同纠纷”或者“农产品(15.65,-0.76,-4.63%)质量不合格引发的赔偿纠纷”为案由提起诉讼。<br />
<br />
　　绿色和平要求沃尔玛返还原告购米款7.2元，立即销毁所有正在销售和库存的转基因大米，建立相关管控体系以避免类似问题再次发生，并通过2家以上的媒体向社会公众正式道歉；<br />
<br />
　　同时，该组织也提请政府相关部门展开从种子源头到米制品的彻底调查，保证整个食物链彻底远离违法转基因稻米的污染。<br />
<br />
　　“由于沃尔玛中国公司总部注册地在深圳，所以首先向罗湖区法院提起诉讼。”据黄琳琳介绍，罗湖区法院已经认可该诉讼可以立案，但是需要补充相关材料，应该在半个月之内可以完成。<br />
<br />
　　急需行政监管体系<br />
<br />
　　“问题的关键是零售商愿不愿意提高监控成本，去对其销售产品进行转基因排查。”黄琳琳表示，“因此，诉讼请求的可操作性尚需讨论并逐步科学化。”<br />
<br />
　　目前可查据的中国转基因大米的生产主要在湖北和湖南，由于转基因大米在生产过程本身就没有公开化，流通也是与非转基因大米混合流通的。因此，违法出售转基因大米的零售很难去溯源责任。因此，最好就是企业在商品采购流程中，有严格的监控机制，防止转基因大米进入超市。<br />
<br />
　　因此，黄琳琳认为，后期监控效率需要两方面促成，一是行政监管体系对零售商严格要求，并有明确的管理与惩罚体制；其次依赖于零售商自律。因此，此次诉讼的结果的执行需要通过政法系统促成行政系统完善对零售商在食品安全方面的管理。<br />
<br />
　　目前，包括家乐福、欧尚、农工商超市等部分大型零售商已经向绿色和平承诺，不销售转基因食品，而沃尔玛及其他很多零售商皆未做出此承诺。</div>

							
							
							
							
