																											<div id="postmessage_753073" class="t_msgfont">作协与鲁迅文学院爆丑闻：年轻女子入作协，先让领导入身体/博讯独家<br />
<br />
博讯记者北京19日得到消息，北京鲁迅文学院又爆出丑闻，年轻女学员在三个月到一年的学习中，平均要陪同老师和作协来的特邀老师睡觉多次，换得可以在有作协资助的国内文学刊物上发表文学作品的机会，以及学校老师和来自作协的特邀老师推荐进入省市和全国作协，成为正式会员。<br />
<br />
鲁迅文学院目前是中国少有的得到国家资助的文学写作者培训基地，每年都有长短培训班，招收全国各地的文学青年。据博讯记者从学院一位山东和一位天津学员处了解到的情况，目前招收的青年学员以“文学女青年”为主，女学员和男学员比例达到3比1。天津的学员是在网络上写了几篇小说后被招进来的，一开始，她以为需要交学费，后来才知道，这个学院有来自国家和作协的资助，主动招收一些有前途的学员。她和去年毕业的来自山东的学员后来相识，两人谈起发表文学作品与进入作协的经历，都发现，睡过她们的作家原来都是鲁迅文学院主动安排的。 (博讯 boxun.com) <br />
两位目前已经发表了文学作品并成为作协会员的女子说，本来在学校的几个月，由于整天讨论文学作品，学员之间就互相混熟了，男女就睡到一起，由于男子少，到学期结束时，还经常交换女友，反正一离开学校，大家都分开了。<br />
<br />
她们说，有几位男老师比较吸引学员，三个月不到，几乎就把班里的女学员都睡过了。去年有一位北京的美女作家竟然被教现代文学的老师玩后庭，玩得到教室后血染牛仔裤。她自豪地对同寝室来自湖北的女作家承认，是那位老师要给她一种“全新的文学体验”，今后会知道什么是“痛”，什么是“快”。天津的女作家说，传授这些中国共产党文学学员女学员如何被性折磨，如何用“三个洞口”伺候文学院的领导和来自作协的老师们，成了鲁迅文学院的基本课程，凡是进修过的女学员，只要没有超过四十，又不是奇丑无比的，几乎都知道这个公开的秘密。<br />
<br />
但被如此玩弄并不是没有报酬的，据说，凡是被玩弄过的女学员，都会很快进入中国作家协会，或者各省市的作协协会，特别是铁凝当主席后，嫌女作家太少，特别少了年轻女孩子，所以鲁迅文学院的老师和作协的男老师们这两年更是加紧用身体吸收女作家。虽然现在已经没有拿国家工资的“专业作家”，但主动找博讯爆料的两位女作家说，进作协还有很多好处，例如组织国内外旅游，只要你和作协的领导，特别是秘书处关系好，每年都有公款旅游的机会，即便不能去国外，也可以在国内玩。上次作协到重庆开会，只是其中一个。（boxun.com) <br />
博讯记者问如何和作协秘书处搞好关系？这两位女作家神秘的说，还是靠关系呗，作协现在是各地政府的花瓶，一些女作家就更是花瓶里的花朵，不但要经常陪同领导“深入生活”“实地采风”，还经常被带去同老板搞关系。说起那些老板，可色了，一听到是作协的女作家，总是很想上的，哪怕那女作家很丑，他们也能翘起来。其中女作家说，有一位老板在八十年代就特别崇拜作家，但没有机缘，后来还是铁凝的粉丝。他和中共高层领导人都有交往，但他说，最想“交配”的人就是中国的女作家，铁凝以前是他梦中情人，但现在显然太老了。没想到，现在竟然能够随便和女作家睡觉，感觉到很兴奋。他目前正在考虑到鲁迅文学院获得荣誉称号，他决定捐一笔钱。他对这位天津的作家说：只当嫖妓的。<br />
<br />
博讯记者了解到，作协每年平均每年都组织60多次出国访问，国内的旅游就更多，绝大部分钱都是政府拨款。上海世博会期间，几乎所有省市的作家协会都拨出资金供作家们到上海采风。<br />
<br />
由于这两位女作家不愿意公布自己的姓名，她们只是说，类似的揭发鲁迅文学院的事已经很多次，但都被作家协会掩盖下去。于是，博讯记者拨通中国作家协会秘书处的电话，希望证实以上消息是否属实。作协秘书处一位显然喝醉了的工作人员说，你有事找铁凝主席。他又恶狠狠的加上一句：你能见到她算你有本事！<br />
