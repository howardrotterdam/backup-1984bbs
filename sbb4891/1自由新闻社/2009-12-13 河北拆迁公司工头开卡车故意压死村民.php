2009年12月12日 星期六 <br />
<br />
河北平泉县地方当局以「三年大变样」名义搞房地产开发，强拆民宅手段蛮横。 12月10日上 午，百名公安、城管强拆村民王雅文的房子，把家中90岁老人下的昏死过去。 10号是世界人权日，王雅文不但房子被强拆，一家人寒冬无家可归，家中老人还被强行抬走不 知去向。王雅文介绍，她家是三代同堂共六口人，居住在286平米的二层楼。政府所给补偿不到 市场价格的十分之一。因拒绝这种掠夺式拆迁，曾遭到停水、停电、堵路、黑社会人员恐吓等。 <br />
