　　6月9日，鹰潭市公安局网络监察支队负责人表示，他们及时找到了组织者，敦促其说服其他网友放弃愚昧行为，而一名已经赶到鹰潭的广东汕头网友，被警方成功说服回家。 <br />
<br />
　　QQ群惊现网友“死亡约定” <br />
　　6月2日，一个心急如焚的报警电话，从安徽合肥打到鹰潭市公安局网络监察支队：“一个QQ群的人商量好了，过几天在鹰潭集合自杀，请务必阻止他们！” <br />
　　胡红成是鹰潭市公安局网络监察支队支队长，也是一名经验丰富的资深网警，但听到这个消息还是不寒而栗。 <br />
　　“根据报警网友提供的线索，我们很快找到了那个自杀群。”参与办理此案的沈警官说，经过查询，民警发现死亡约定发布于6月2日晚，提议者的网名叫做“沙沙”，原文内容是：“5日晚8时，江西鹰潭火车站对面夜猫网吧集合，一起上庐山，看完日出就走！” <br />
　　这个恐怖的约定发布后，竟然引得一些网友响应。 <br />
<br />
　　民警进自杀群劝阻轻生 <br />
　　为了阻止悲剧的发生，6月2日深夜，沈警官和其他三名同事分别申请加入QQ群，准备通过私聊的方式各个“击破”。 <br />
　　可能是几名民警的加入，引起了群内网友的警觉。6月3日上午，网友“沙沙”另建了一个自杀群，继续讨论如何到鹰潭集合，通过什么路线上庐山。 <br />
　　为了随时关注群内网友的动态，4名民警24小时轮流值班，对两个自杀群进行不间断监控，并且向省公安厅请求协助。 <br />
　　“要制止此次自杀行动，关键是找到牵头人和组织者。”胡红成说，他们一边监控一边将工作重点放在寻找“沙沙”上。 <br />
　　6月3日下午，在省公安厅的帮助下，网络警察很快查出“沙沙”是上饶人。 <br />
　　群主幡然醒悟中止行动 <br />
　　6月4日上午，鹰潭市公安局网络监察支队的民警找到“沙沙”的家时，他已经被姐姐“软禁”在家里。原来，姐姐已经发现他的异常，正苦口婆心地劝说弟弟不要做傻事。 <br />
　　这名24岁的男青年由于性格内向，找工作一直不顺利，想到这么大了，还在拖累父母、亲人，竟产生轻生厌世的念头。一个偶然机会，他建立了一个自杀QQ群，没想到得到一些网友的响应。 <br />
　　民警与“沙沙”谈心，人生遇到挫折是很正常的，只要树立生活的勇气，没有什么困难不可以克服。经过两个多小时的促膝交谈，“沙沙”打消轻生念头。 <br />
　　从当天下午开始，警方发现，QQ群已经被关闭，“沙沙”通过私聊和发帖的方式请大家停止行动，重新树起生活信心。<br />
 <br />
　　广东网友赶到鹰潭被劝阻 <br />
　　然而，不是所有的人都得知了自杀行动取消，5日中午，一名来自广东汕头的年轻人迫不及待地赶到了鹰潭火车站，他的网名叫“寒风凛冽”。 <br />
　　“由于夜猫网吧当天歇业，下午5点左右，我们发现‘寒风凛冽’在隔壁的巅峰网吧上QQ了。”沈警官说，为了防止打草惊蛇，几名警官隐藏身份在线对他进行说服。 <br />
　　“寒风凛冽”是“90后”，父母关系一直不好，很少感受到家庭的温暖，前不久，女朋友突然离他而去，让他觉得世界末日来临。 <br />
　　民警们以兄长的身份，劝说了3个小时，这名1992年出生的大男孩终于开口说“我不打算自杀了”。 <br />
<br />
　　深圳警方也在行动 <br />
　　当晚21时左右，“寒风凛冽”突然下线离开网吧，民警们心里倏地一紧。 <br />
　　“我们一直密切观察着他，防止他一时想不开再次决定轻生。”沈警官说，“最后我们发现他打了个电话给在广东的姐姐，让她汇钱来，说他想回家了”。 <br />
　　由于老QQ群封闭，在新成立的群中，鹰潭网警连续观察几天后，发现群中网友大多数表示醒悟过来，不想做愚鲁之事了。 <br />
