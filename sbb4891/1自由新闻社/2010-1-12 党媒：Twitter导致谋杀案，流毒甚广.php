																											<div id="postmessage_427292" class="t_msgfont">美国爆出首起“Twitter谋杀案”<br />
<br />
美国警方近日正在调查一起凶杀案，他们认为，这起凶案是由于受害人和凶手在微博网站Twitter上留言发生争执后引起的。这也是Twitter诞生以来，首起“Twitter谋杀案”。<br />
<br />
“网络骂战”演变为凶案<br />
<br />
美国纽约警方称，凶手杰姆格・布雷克和受害人克瓦姆・丹西是朋友，两人在微博网站Twitter上互相发送带有敌意的评论已经有一段时间了。最终，这种“网络骂战”演变为凶杀案。<br />
<br />
据警方透露，22岁的凶手布雷克和22岁的丹西从小就相识，他们住在同一幢高层公寓的同一个楼面上，但两人因为爱上了同一个女孩而分道扬镳。从那以后，两人就一直在Twitter网站上互相发送恶意留言。而他们两人的其他朋友则在网上“煽风点火”。<br />
<br />
2009年12月1日，布雷克还向丹西发送了一条带有威胁性的Twitter留言。当天，丹西在离开住所后被子弹击中颈部，随后死亡。丹西生前正在接受训练，他即将成为一名护士。<br />
<br />
在丹西死亡后的第三天，布雷克在自己的Twitter账号上发布了这样一条消息：“愿克瓦姆的灵魂得到安息。 ”<br />
<br />
两人从小就是好友<br />
<br />
丹西的母亲玛德琳・史密斯说，两个朋友之间在网络上的争执竟然能演变成为一起凶案，这让她十分震惊。“这并不能构成一个用枪指着别人的理由，”她说，“这实在是太疯狂了。我不知道那个叫做Twitter的玩意儿上面到底发生了什么事情。 ”<br />
<br />
史密斯说，她的儿子和布雷克从小就是好朋友，“很显然，我并不像我以为的那样了解他，我只想问他，‘为什么你要这样做？你怎么下得了手的？ ’”<br />
<br />
Twitter留言首次被用作凶案证据<br />
<br />
警方已经向Twitter网站提交了执法申请，希望能得到两人之间互发的所有信息。罪案专家称，这也是谋杀诉讼中首次运用到Twitter网站上的留言作为证据。公诉人将会用这些留言来说明，凶手对受害人的敌意是如何逐步加深的。<br />
<br />
根据警方得到的监控录像显示，布雷克曾经拎着一个足以放下作案用枪支的包离开自己所住的公寓。而目击证人也指认称，布雷克就是那个向丹西开枪的凶手。<br />
<br />
打死丹西所用的枪支后来被发现遗弃在纽约的中央公园内。<br />
<br />
布雷克目前已经被起诉犯有一级谋杀罪。<br />
