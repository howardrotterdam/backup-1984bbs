央视在NBA和CBA转播中已经进行了修正，4月4日的CBA季后赛半决赛中，央视的解说中已经规避了“CBA”，直接称比赛为“中国男子职业篮球联赛”。据悉央视已经接到有关部门的通知，在以后的转播中必须屏蔽一些外文缩略词，例如“NBA”、“CBA”和“F1”……<br />
看到这个标题的读者必然有些纳闷，是不是记者和编辑写错了？没错，央视日前接到有关部门下发的通知，要求在央视今后的转播中必须要屏蔽一些外文的缩略词，例如“NBA”、“CBA”和“F1”等，一定要说的话，就说赛事的中文全称。NBA以后叫“美国职业篮球联赛”，CBA叫“中国男子篮球职业联赛”。<br />
避免使用英文简称<br />
<br />
通知中，要求主持人口播、记者采访和字幕中，不要使用外语及缩略词，如NBA等。如遇特殊情况必须使用，要在外语及外语缩略词后加中文解释。实际上这次通知还不仅仅限于体育领域，如GDP、WTO、 CPI等也在“整顿”范围内。<br />
<br />
近几日，细心的球迷不难发现，央视在NBA和CBA转播中已经进行了修正，4月4日的CBA季后赛半决赛中，央视的解说中已经规避了“CBA”，直接称比赛为“中国男子职业篮球联赛”。实际上，央视在体育新闻中早已经规避了“CBA”一词，从来都是叫比赛的全称。一位电视主持人表示：“这个就是个称呼 。具体为什么不让说英文缩略词，我也不知道，对我们来说无所谓。我也在尽可能地规避一些简称。”<br />
<br />
也许是新规定没有坚决贯彻，昨天央视18时的体育新闻中，播音员吴晓琳就再次说出了“NBA”。而在她播报新闻时屏幕右上角的提示小方块中，还写着“NBA常规赛”。<br />
<br />
新规定不仅局限于央视<br />
<br />
央视不让说，地方台是不是也需要改动呢？<br />
<br />
实际上一些省（直辖市）电视台早在上周就接到了相关通知。<br />
<br />
据了解，吉林电视台上周就接到了相关的通知。一位主持人解释道：“倒不是坚决地不让说，如果你要是说就需要在英文简称后，加上中文的全称。例如，CBA可以这么说‘CBA中国男子篮球职业联赛’ 。”<br />
<br />
北京电视台关于CBA和NBA的节目预告，也已经改成“中国男子篮球职业联赛”和“美国职业篮球联赛”。<br />
<br />
●连线<br />
<br />
央视解说员于嘉：我还挺适应<br />
<br />
既然有这个规定，就需要主持人落实。央视的体育评论员于嘉表示：“这个规定我还挺适应”。<br />
<br />
于嘉介绍，这个规定我们已经知道一段时间了。“在比赛中，我们也进行了落实，NBA和CBA的字眼已经不再提了。NBA我们现在就叫‘美国职业篮球联赛’”。至于这种新叫法是否适应，他表示：“我还是挺适应的，没有什么特别的。”<br />
<br />
于嘉表示，实际上，解说员和记者在比赛转播中规避起来也比较简单。基本上只是在节目一开始说上一句，其余时间也不会总使用NBA和CBA。</div>

							
							
							
							
