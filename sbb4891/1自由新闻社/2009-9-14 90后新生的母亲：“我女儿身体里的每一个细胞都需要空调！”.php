　　&quot;我女儿身体里的每一个细胞都需要空调！为什么不能给新生寝室装空调？&quot;日前在武汉大学校长顾海良与近400名新生家长的对话会上，一位母亲泪流满面的诉说引来在场家长的一片热议。无独有偶，华中科技大学的一位新生家长也向学校报怨，哭诉女儿的宿舍条件太差，厕所蹲位没有隔门，孩子连着三天都没好意思在里面大解，根本没办法在那里生活。女儿很失望，想复读重考一个大学。 <br />
<br />
　　今年，随着90后大学新生大量进入高校，不少新生家长自认为是非常&quot;基本&quot;的要求，在高校、社会和网络上引发热议。是90后的新生太娇贵？还是大学的生活条件跟不上社会进步的步伐？家长们越来越多的&quot;干预&quot;也正在挑战着新时代高校的管理和人才培养工作。<br />
<br />
　　空调、厕所、热水、隔门全成了问题 <br />
<br />
　　武大新生报到后的第一天，学校举行了一场“校长家长面对面”活动，校长顾海良带领教务、学工、招生等部门负责人与近400名新生家长进行了交流，对家长们关心的问题进行了一一解答。 <br />
<br />
　　“我的女儿一出生，我们家就花2000多块钱装了空调，从小她都已经习惯了。生活里就是吃不上饭也少不了空调。没想到我们昨晚到武大后，在宿舍里睡了一夜，我女儿一起来就对我说，'妈妈，我觉得好像火从被子烧到心口了'……”说到这里，这位家长哽咽得说不下去，当着武大校长顾海良和近400名学生家长的面，泪流满面。 <br />
<br />
　　这位母亲激动的哭诉，引发会场一片哗然，有家长鼓掌表示支持，有家长低头议论纷纷。“都什么年代了，空调这样的基本生活设施为什么都满足不了？”“好夸张，有这么娇贵吗？”一位新生父亲说，孩子吃点苦有好处。旁边的一位家长却说，“现在生活水平提高了，让孩子生活环境改善一下也不是什么大错，武汉实在太热，我们家长也心疼。”另一位母亲跟着说，“孩子高中时宿舍都装了空调，到了大学反而没有了！” <br />
<br />
　　现场一位大三同学表示，武汉的夏天是比较热，但最热的时候学校其实已经放暑假了。“热”也是一种难得的生活经历，很能锻炼人的。“以后毕业了，想起室友们一晚洗几个澡的经历也还蛮开心的。”旁边的一位家长却反驳说，社会是要进步的，现在又不是没有条件，实在不行，自己出钱装一个。 <br />
<br />
　　在华中科技大学，虽然开学仅四天，新生家长黄女士已经往学校主管部门跑了两趟。她说，女儿所在的宿舍楼每层只有一个卫生间，每天早上上厕所得抢位置。因为蹲位没有隔门，孩子连着三天都没好意思在里面大解。洗澡间不但没有隔门，还没有热水。开水房又比较远，孩子提两瓶水怎么提得动？宿舍的纱门、纱窗破洞也不少…… <br />
<br />
　　在宿舍里住了三天后，黄女士的女儿实在无法适应，她发短信和打电话对妈妈说：“我非常失望，我要复读，重考一个大学。”对此，黄女士非常难过，她说：“孩子从没吃过这种苦啊！不管住宿费有多贵，只要我的孩子对住宿条件满意，我都能接受。” <br />
<br />
　　“没有一种菜不是辣的”，来自香港的经济系学生黄同学的母亲也很担心女儿以后的生活。在武大的报到现场可以看到，一个学生至少有1名家长陪同，最多的竟有6名家长、亲戚一起送来。更有甚者，据当地媒体报道，武汉某部属高校有一位“富家女‘在父母陪同报到时，坐加长轿车，携带了19箱行李，还配备了2名专职导游。这样的出行阵势把迎新的同学惊得目瞪口呆，其家长甚至要求在学校的寝室中加装独立卫生间。 <br />
　<br />
&nbsp; &nbsp;&nbsp;&nbsp;90后最大的问题仍是独立人格的培养&nbsp;&nbsp;<br />
<br />
&nbsp; &nbsp; 2009年9月7日，华中师范大学开学首日，为新生家长们提供免费住宿。晚22时许，在华中师范大学佑铭体育馆内，贫困生的家长入住校方免费提供的室内大地铺上。<br />
<br />
　　2009年9月7日，华中师范大学开学首日，为新生家长们提供免费住宿。晚22时许，在华中师范大学佑铭体育馆内，贫困生的家长入住校方免费提供的室内大地铺上。<br />
<br />
　　与80后相比，90后是我国更为纯粹的一代独生子女。虽与80后一样出生在中国改革开放之后，但90后出生在我国经济起飞时期，与80后及此前出生的70后相比，他们大多无需面对生活的磨难，优越的家庭环境和社会条件也使他们的成长道路大多较为平坦和顺利。 <br />
<br />
　　武汉大学校长顾海良介绍说，今年武大的新生中，89％以上是&quot;90后&quot;，而去年这一比例仅仅是45％。这代独生子女从小在家里备受呵护，在生活上依赖性很强。顾校长举例说，去年学校有个新生，家长在临走前给他买了30件衬衣，还编好号几月几号换，让孩子按日子穿完，放假一起带回家洗。还有位母亲每隔一段时间就来学校给孩子洗衣服。 <br />
<br />
　　荆楚网网友叶青林指出，大学新生入学，家长簇拥、公车相送、豪华礼品这些都已算不上新闻，对于真正成长在幸福和甜蜜中的90后而言，这些似乎都应该是理所当然。如今，武大新生家长哭诉学生寝室无空调一事，更是让我们认识到，孩子只是&quot;被娇惯&quot;，而家长的溺爱才是造成这些大学新生娇贵的根源所在。如今孩子们有了接受锻炼、学会成长的机会之时，首先出来诉苦的不是新生自己却是不堪忍受孩子吃苦的家长。如此一来，孩子不仅失去了自我锻炼的机会，家长更成为了孩子成长成材道路上的绊脚石，这是何其的得不偿失。<br />
<br />
　　叶青林表示，&quot;自古英雄多磨难，从来纨绔少伟男&quot;。试想，一个连一点苦都吃不了的大学生，那么将来他又该用什么来面对职场的残酷竞争和生活的巨大压力。古人为了学习头悬梁、锥刺股以&quot;自讨苦吃&quot;，而今天的新生却对&quot;吃苦&quot;一事唯恐避之不及，想必这就是新生们饱受诟病的根结吧--习惯娇贵，吃不了苦，不愿吃苦。<br />
<br />
