―― 一位网站监测工作人员的道白<br />
<br />
　　……各个网站都有关键词过滤和固定的删帖的人员。我们公司就有十几个（人），人工看。而且他是要所谓“纳入有效的管理”。中国的前20大网站他已经纳入到一个固定的，日常化的管理中了。它会有一个程序，它告诉你说又发现了某一个人，发现了哪一条，马上删掉。他有一个后台，他是有通知的。后来他就不敢用这种后台了，怕暴露。怕你输入密码就进入他的管理后台。当然后来他就逐渐成了地下工作者，给你发个短信或者电话通知。这是网监处。 <br />
　　还有一个是网管处，每周他会告诉你：最近我们应该管什么事儿，哪个网站做的不好，哪个网站违规了。他还做了一个处罚条例。这在全世界都算得上是一个很莫名其妙的处罚条例，很大，评价条例有上百条吧。比如说你不参加我开的会我给你扣多少分儿；你没有发布指定的新闻扣多少分儿。当然他还有做的比较绝的是你主动地在份内时间里多做了几个专题就会为你加分，他也不会把你一下子弄死。反正把你闹得自己很难受。最后呢他要公布谁是第一名，谁是第二名。<br />
　　每周五都必须有一个人去北京市新闻办开会，这个人叫第一通知员，为什么呢？因为他找不到人，都不愿意去，他就规定第一责任人或者第一通知员必须24小时开机。一个是监管的负责人一部电话，他要和他们之间对话，还有这个第一通知员，也就是一个小头目，他是双层管理，这个人也要负责管。这是两条线来控制。<br />
　　这是日常管理，这个日常管理在正常时期尚可接受，那么在突发事件时期他就采取突发手段。比如说上头开会时期或者某个特定的日子，他是要求万无一失。平时我们做的相对比较松的比如跟帖呀什么都可以讲的，但一到敏感日，其实是相互都紧张了。这边很紧张，那边也很紧张。他会调用你的编辑去那边上班，就和征用你的编辑一样。你必须派俩人来上班。我们直接处理，当场看到当场处理，也就是他临时组建这样一个队伍。另外一个就是比如跟帖呀，这些是很难控制的，干脆全部关闭，像论坛和显眼的版块，谁出了问题他会整你呀。敏感时期，你就是说好话都不行，比如说某某某人好呀，不行，这些人都成敏感词，干脆你就上不去。他就是要确保这个期间万无一失。只要过了这个时间大家都好过，要是过不了你就倒霉。因此他还是有很大的掌控力的。这就是他日常和非常时期做的事情。他是直接受国务院新闻办管。在中国日常管得最严的还是新闻办这个系统。<br />
　　上边说的是主线，那么还有两条分线，一条是信息产业部的，就是电信管理局，他是基本上负责ICP的备案。还有一条就是公安的网监系统，他也有一条庞大的网络监测系统。他基本上是这样三条线。当然他还会通过各种渠道，这些部门的渠道是出于利益的保护，但日常的还是那三条主线。<br />
　　关于监控的事情，其实他一直在网端有监控，比如和思科呀这种，我们只是不知道而已。这是他的第一层监控。<br />
　　现在一些破网软件太厉害了，它能给你绕过去，你也不知道是谁？他是怎么绕过去的，这个监控基本上是没用了，就是所谓金盾工程基本上是没有用了。<br />
