　　在中国大陆网络文化中，有网友经常把北京方言中的一句骂人话简化成：“SB”，有网友据此调侃此事，称google此举是在骂百度，但也有资深域名专家向新民网记者称，“SB”仅仅是一个国际顶级域名，google抢注baidu.com.sb应该是出于商业考量。<br />
<br />
　　而据此间不愿具名的知识产权律师向新民网记者介绍，近年，企业之间的域名争议诉讼越来越多，如美国杜邦公司就dupont.com.cn域名诉北京国网公司案件，最后北京法院判处后者撤销域名注册。该律师称，百度如就google此举进行诉讼，胜诉的可能性较大。<br />
<br />
