<br />
　　根据网友描述，新民网记者登陆了“涉嫌”抢注的域名<a href="http://www.baidu.com.sb" target="_blank">www.baidu.com.sb</a>，该页面迅速指向google的全球页面<a href="http://www.google.com/" target="_blank">http://www.google.com/</a>，记者随即查证到，<a href="http://www.baidu.com.sb" target="_blank">www.baidu.com.sb</a>的IP指向： 64.233.189.104，按照公开数据，为美国加利福尼亚州山景市谷歌公司所在地。而通过全球域名管理机构ICANN查询，sb域名是所罗门群岛国家顶级域名。另外新民网记者也注意到google还持有<a href="http://www.google.com.sb" target="_blank">www.google.com.sb</a>域名。<br />
