													<h2>安徽淮南一城管局长被违建户主推下二楼身亡</h2>
						
						
																											<div id="postmessage_818624" class="t_msgfont">中安在线9月8日报道&nbsp;&nbsp;9月7日下午4点左右，(安徽)淮南市田家庵区城管局对当地一处违建进行强拆。行动过程中，正在现场负责指挥的田家庵区城管局局长被人推下二楼，不幸身亡。<br />
<br />
巡查中发现违建<br />
<br />
据有关部门负责人介绍，当日16：20左右，田家庵区城管局执法人员正在当地国庆西路巡查。其间，带队局长、党委书记吴建国发现沿街有一户违法建设正在抢建，吴建国及20多名执法人员当场进行制止。户主尹某某闻讯赶到现场，试图阻拦。通过耐心说服教育，尹某某当时已经停止阻拦。<br />
<br />
户主突然将局长推下楼<br />
<br />
吴建国局长随后带领执法人员实施拆除行动。当拆除进入尾声时，户主尹某某突然将正在二楼楼顶拆违的吴建国推下楼。吴局长摔到地面当场昏迷不醒，随后紧急送往淮南市第一人民医院紧急抢救无效后不幸身亡。<br />
<br />
据悉，此前田家庵区城管执法人员已多次制止该户主尹某某违法抢建行为，但尹某某始终断断续续进行偷建抢建，目前公安机关已控制尹某某，善后各项工作正在有序进行中。<br />
<br />
还有一名人员重伤<br />
<br />
昨天晚上9点左右，在淮南市第一人民医院门口停放着几辆贴有行政执法字样的车辆。在医院大院内，聚集着很多当地城管部门的工作人员。记者随后从医院方面了解到，当天下午5点左右，他们接到伤者，除了确定吴局长经抢救无效身亡外，还有一名重伤人员也同时被送往医院。据医生介绍，这名伤者正在ICU重症病房接受治疗，身份尚未确定。<br />
<br />
局长曾多次带队拆违<br />
<br />
据了解，吴建国局长今年曾多次带队到第一线指挥拆除违章建筑。事情发生前不久，田家庵区城管部门举行“大干八十天重塑新形象”活动，对单位工作人员制定了相关禁令，并成立督查小组，由该局局长吴建国同志任组长。<br />
<br />
(本文来源：中安在线 作者：欧阳徐中)</div>

							
							
							
							
