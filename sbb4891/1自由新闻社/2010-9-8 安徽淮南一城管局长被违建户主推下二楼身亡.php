<br />
吴建国局长随后带领执法人员实施拆除行动。当拆除进入尾声时，户主尹某某突然将正在二楼楼顶拆违的吴建国推下楼。吴局长摔到地面当场昏迷不醒，随后紧急送往淮南市第一人民医院紧急抢救无效后不幸身亡。<br />
<br />
据悉，此前田家庵区城管执法人员已多次制止该户主尹某某违法抢建行为，但尹某某始终断断续续进行偷建抢建，目前公安机关已控制尹某某，善后各项工作正在有序进行中。<br />
<br />
还有一名人员重伤<br />
<br />
昨天晚上9点左右，在淮南市第一人民医院门口停放着几辆贴有行政执法字样的车辆。在医院大院内，聚集着很多当地城管部门的工作人员。记者随后从医院方面了解到，当天下午5点左右，他们接到伤者，除了确定吴局长经抢救无效身亡外，还有一名重伤人员也同时被送往医院。据医生介绍，这名伤者正在ICU重症病房接受治疗，身份尚未确定。<br />
<br />
局长曾多次带队拆违<br />
<br />
