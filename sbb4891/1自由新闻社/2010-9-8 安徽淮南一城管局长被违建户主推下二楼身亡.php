<br />
巡查中发现违建<br />
<br />
据有关部门负责人介绍，当日16：20左右，田家庵区城管局执法人员正在当地国庆西路巡查。其间，带队局长、党委书记吴建国发现沿街有一户违法建设正在抢建，吴建国及20多名执法人员当场进行制止。户主尹某某闻讯赶到现场，试图阻拦。通过耐心说服教育，尹某某当时已经停止阻拦。<br />
<br />
