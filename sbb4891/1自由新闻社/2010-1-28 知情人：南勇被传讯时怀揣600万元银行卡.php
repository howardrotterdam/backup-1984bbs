																											<div id="postmessage_455393" class="t_msgfont">揭秘足协高官腐败生活：某教练送50条中华烟挨骂<br />
<br />
2010年01月28日09:02<br />
<br />
　　从南京的天价局长周久耕，到内蒙古的百万豪车检察长刘丽洁，“露富”成了纪检部门和社会舆论反腐的突破口。在“腐烂到根”的中国足球圈，类似的 “周局长”和 “刘检察长”也不乏其人。与周局长手腕上的江诗丹顿类似，2001年十强赛后，某足协高层曾戴过金光闪闪的劳力士；与刘检察长“借用”的百万豪车相同，2005年标致307风靡大陆之时，某足协高层在一位国字号教练的帮助下，开上了普通人排队3个月也等不上的红色新款。<br />
<br />
　　因劳力士被曝光而大骂记者<br />
<br />
　　用“黑金”这一政治腐败现象，来形容处处凸显“行政足球”的足协管理部门，非常贴切。这些“黑金”的来源首先是国字号教练、然后是国字号球员，最后则是俱乐部。中国足协能够直接控制的就是国字号教练，一直以来都把国字号教练当作“足协自己的教练”。<br />
<br />
　　2001年十强赛出线之后，国内足球一派喜象，足协某高层出席活动时，手腕上的劳力士金光闪闪，当时有人无意中拍下来，事后向该高层求证。意识到事情的严重性，当事人连口解释，“假的，仿制的，我怎么可能有钱戴那种名贵的表呢。”后来，这名高层和好友在酒桌上说起此事破口大骂：“X的，记者管闲事的真多，像狗一样什么都能闻到。”从那以后，那块不知“真假”的劳力士，被他的主人忍痛“封杀”。<br />
<br />
　　当时的“人肉搜索”远不及现今之疯狂，除了劳力士，该高层一身的名牌也让他人瞠目结舌。有一次，他到外地出差，接待方很“识货”，发现领导身上的衣服和鞋子的市价超过3万元，于是在席间调侃一句，“很名贵啊，只能在境外才能买到啊。”该高层甚是尴尬：“嗨，不贵，秀水摊上买的。”话虽如此，那些衣服同样也“消失”了。<br />
<br />
　　中华烟一送就是50条<br />
<br />
　　抛开衣食住行，足协大员在其他方面的花费也非常惊人，特别是家人出国等方面。当然，不能否认，某些家属确实有其他合法的经济来源。只是大家不明白，一些在体院当老师平时兼职裁判的“名哨”，却住着价值几百万的豪宅，开着几十万的豪车。<br />
<br />
　　据传，从2001年十强赛时起，就有教练为了进入国X队，不惜“奉献”出自己的箱底。送烟、送劳力士、送LV包、送珠宝首饰，不尽其人。一名执掌过国青和国少的教练，曾给足协高层送过一整箱高档中华烟，足有50条之多。这一“阵势”把该高层都吓坏了，抬脚把箱子踢破，张口大骂，“你他X想害死我啊！”吓得该教练只好当晚雇出租车，连夜返回住所。<br />
<br />
　　某俱乐部每年“上贡”至少20万元<br />
<br />
　　北方某教练在俱乐部执教时，“秉承”该俱乐部的旨意，每年年初和年底都会拜访某高层，每次大约在10A到15A，了解赌球的人都清楚，A实际上代表的是万。这名年轻教练后来也进了国X队教练组。只不过，这名教练还不错，在国X队时，他没有权力也并未靠球员来“反哺”。<br />
<br />
　　2005年之前，标致307轿车一度在中国很抢手，尤以红色款为甚，普通人排队3个月也没有货。某高层据说是要为“朋友”买一辆红色新款，动用了在武汉的不少关系，依然没妥。据传，一名教练知情后挺身而出，拔刀相助。没过多久，最新潮红色标致307的钥匙，便交到高层手中。<br />
<br />
　　而那名教练，也如愿在国字号球队“混”了一段时间。当时，几乎所有业内人士都很惊诧，“这样的人也能当国字号队伍的教练？”再后来，大家终于明白，那名教练为什么每次竞聘之前都意气风发，志得意满。当然，那辆红色标致307依然是个谜局，也存在一种可能：该高层自己付钱，该教练利用关系在武汉为他搞到了紧俏型新款。<br />
