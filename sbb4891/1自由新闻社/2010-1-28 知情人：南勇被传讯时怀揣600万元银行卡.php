																											<div id="postmessage_456948" class="t_msgfont">曝南勇被传讯怀揣600万元银行卡&nbsp; &nbsp;300万来历不明<br />
<br />
2010年01月29日07:48<br />
<br />
<img src="http://img.hexun.com/2010-01-29/122530243.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
南勇（资料图）　<br />
<br />
　　尽管南勇身犯何罪公安部至今未曾公布，但记者28日从知情人处获悉，南勇所涉案的金额远不止媒体披露的数目，令人惊诧的是，在被传讯时南勇身上的银行卡居然有600万之多。正是这张有着600万的银行卡击溃南勇一度固守的心理防线，从而迅速招供。<br />
<br />
　　在被专案组传讯之前，南勇可能有思想准备，但是没想到一切会来得这么快。15日，南勇像以往一样上班，并且可能有了将财产转移的设想。不过，当总局要求他去开会的时候，他暂时搁置了想法，毕竟一切看起来都还算平静。可惜人算不如天算，当南勇和杨一民一起到了总局，才发现自己中了“埋伏”。也许在那个时候，他就知道自己完了。<br />
<br />
　　没有任何的侥幸，反赌专案组很快就查出南勇的银行卡上有600万的巨款。这对于南勇来说，是一笔说不清楚的巨款。毕竟在1998年进入足协之前，南勇的收入并不丰厚。即便是进入足协后，每个月工资也不会上万，这样12年下来，总的收入最多不过100万出头而已，距离南勇卡上的600万还有着很大的差距。<br />
<br />
　　银行卡是南勇的，但钱的来源是需要他交代的。于是此前口风还算很严的南勇迅速招供，而且招了很多，目标直指国字号和中超。从目前来看，即便媒体披露的南勇收受国安50万和私吞哈恩薪水中的15万美元属实的话，折合人民币也不过150多万，这意味着至少南勇银行卡上还有将近300万元人民币是来源不明的。<br />
<br />
　　如果最终落实南勇确实是商业贿赂，那么南勇将受到怎样的法律制裁呢？某专业律师在接受记者采访时表示：“现在到了刑事拘留阶段，意味着已经有证据证明他们触犯了法律。如果是受贿1万元，那么他将可能被判处1年有期徒刑。如果受贿超过1万元以上，他就有可能被判处10年或者10年以上有期徒刑。如果受贿超过6万元，他将有可能被判处10年以上有期徒刑。如果再超过的话，无期或死刑都有可能。”<br />
<br />
　　记者还从特殊渠道了解到，目前位于北京的南勇等人的住宅及办公室已经被警方搜查。据称，来北京进行搜查的警察就是现在在足坛人人谈之色变的 825专案组的警察，这些警察持有合法的搜查手续，并且已经搜查了南勇等人的住宅及办公室。<br />
