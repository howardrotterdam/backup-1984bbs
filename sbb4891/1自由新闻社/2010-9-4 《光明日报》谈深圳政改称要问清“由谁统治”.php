																											<div id="postmessage_812979" class="t_msgfont">2010年09月04日 光明日报<br />
<br />
在纪念深圳经济改革30年之际,深圳在政治改革方面的作为也颇受人关注。很多人认为,深圳不仅是经济改革的急先锋,也是政治改革的试验田。毫无疑问,从社会主义民主政治发展的广阔视野来看,深圳在政府机构设置、权力划分、人员配置等方面的改革尝试,为拓宽中国特色社会主义政治发展之路,积累了点滴经验,提供了宝贵借鉴。就此而言,30年来,深圳经验不仅是“经济”的,也是“政治”的。<br />
<br />
不过,在深圳政治改革经验带来的期待之中,尤其是关于政府权力的划分方面,有些认识较为模糊,甚至有混淆概念、盲目比附之嫌。比如,有人会认为决策权、执行权、监督权的三分,类似于西方政治中的“三权分立”,但范围有限,不够清晰。这种简单的比较,着重于形式,忽略实质内容,很容易产生误会。究其根源,原因在于此类观点没有分清社会主义民主与资本主义民主的界限,硬用西方的概念套中国政治发展的现实。<br />
<br />
基于现实政治问题,作一些比较研究并无不可,但前提是要划清两种民主的根本性质;否则,就难免张冠李戴,得出不当结论。<br />
<br />
首先,在回答“由谁统治”这个问题上,社会主义民主与资本主义民主有着截然不同的答案。社会主义民主,坚持实行人民民主专政,真正实现了人民当家作主,既在人民内部实行最广泛的民主,又依法对极少数敌人实行最有效的专政;而资本主义民主则是少数人的民主,局限在资产阶级内部,本质上就是为了保护生产资料私有制、实现和保障资产阶级利益。这种“国体”上的差别至关重要,体现了民主政治鲜明的阶级性。因而,如果有人在没有认清“谁来统治”这个问题之前就去讨论具体政治问题的是非,必定是盲人摸象,错漏百出。<br />
<br />
其次,社会主义民主与资本主义民主在“如何统治”的问题上也大相径庭。社会主义民主坚持人民代表大会这一根本政治制度,充分保证了人民当家作主的主体地位,充分发挥了人民群众的主人翁精神;而资本主义民主虽然也标榜“主权在民”、“人人平等”,但在私有制条件下,不论制度形式如何变换,其实质仍然是少数人的轮流坐庄。这种政体上的差异,是我们观察现实的重要背景,倘若抽掉,必会导致偏颇以至荒谬的判断。<br />
<br />
另外,除开国体和政体上的区别,社会主义民主和资本主义民主在政党制度、民族关系和基层民主等问题上皆有显著差异,同样不可忽略。所有这些不同之处,都是我们进行抉择和改革的基石。无论是一个地方的试点也好,还是全局性的尝试也罢,都应基于对两种不同性质民主的清晰区分和判断之上。</div>

							
							
							
							
