<br />
应邀在纽约参加一场数码社运和民间媒体研讨会的中国艺 术家艾未未，周二会上向Twitter创办人多尔西（Jack Dorsey）提出s“是否有可能提供Twitter中文版服务？我需要一个明确答案，有或没有？”多尔西在电话会议中的答复是s“有，这只是一个时间问题。”多尔西表示在3周前才得悉Twitter在中国大陆被封锁，而正在确实推动中文版，即使面对中国政府的严厉审查规管，也力争让一些有法子突破防火墙的中国民众可以使用中文畅所欲言。<br />
<br />
艾未未对于答复非常满意：“他是非常认真清楚的一个人，当然不能随便作答复，我对这个答复是很满意的。”<br />
<br />
记者：他说也是近期才知道中国大陆不可以自由上twitter的情况，你觉得这个反映了什么？<br />
<br />
艾未未：我觉得国外大多数民众或媒体对中国处在一种什么情形下还不是很清晰，他们不知道为什么上twitter上youtube会有问题，这实在是很多人不能理解的事情。<br />
<br />
Twitter近年在伊朗等地的社会、民主运动中起了重要的作用，在中国也受到知识分子以及技术高端群体的热捧，但由于大陆的封锁以及没有中文版本，局限了Twitter使用者的范围，中国本土的微博客如新浪微博则在短时间内攻占市场。而艾未未认为新浪微博不能与Twitter相提并论。<br />
<br />
记者：你觉得Twitter推出中文版对中国有什么影响么？现在新浪微博好像也发展得很快。<br />
<br />
艾未未：新浪微博完全没有任何伦理道德支持，简单的配合一个非常反对进步和反对社会向更好方向发展的商业载体，它是这类载体的代表。当然网络自由、信息和言论的公开化，是社会进步首要的条件。<br />
<br />
记者：现在谷歌都想退出中国，你觉得Twitter中文版会不会也遇到阻碍。<br />
<br />
艾未未：这个肯定会有问题。人类整个文明史都会有进步的力量和试图保守力量的争斗，但我们还是觉得社会会进步，不管在某一个阶段、某个区域显示的并不是这样。<br />
<br />
记者：你这次在美国参加一些互联网自由会议，也接受了一些采访，希望通过这些对中国互联网现状起什么作用呢？<br />
<br />
艾未未：我们每个人都在做自己的努力，我是个艺术家，我必须坚持言论自由和表达的权利，不同的人在作不同的努力。当然我从中学到很多，我发现所有人都愿意看到一个社会进步，即使不熟悉的人都愿意去了解和帮助，我觉得这个是非常重要的，可以看到大家都很真诚地对待这样的问题，希望能发现一个可行的方式。<br />
<br />
而据《美联社》和《法新社》引述Twitter另一创办人威廉斯周二称，正是资讯审查国家的防火墙，促使网络革新。他说s互联网是一股无人能阻挡的浪潮，它关乎任何人都可以与世界分享的资讯民主，未来数十年将继续改变制度。</div>

							
							
							
							
