													<h2>Twitter正在建设中文版</h2>
						
						
																											<div id="postmessage_518800" class="t_msgfont">Twitter创始人之一多西(Jack Dorsey)美国东部时间周一夜间表示，这家网站正在采取措施让中国用户能够以中文界面登陆。但这家备受欢迎的网站在中国仍然处于被屏蔽之中。<br />
<br />
多西在纽约的一次讨论会上表示，Twitter正在“努力”让用户能够以中文注册。他是在回答中国先锋艺术家艾未未的一个问题时做出这番表示的。<br />
<br />
艾未未一直对中国有关部门及其持续的审查制度予以公开批评。他称自己每天约有八个小时都用在Twitter上。<br />
<br />
艾未未是通过卫星参加此次讨论的。他问多西，我要你明确答复，可行还是不可行？<br />
<br />
多西回答说，可行，只是一个时间问题。他指出，建立中文注册页面存在人手不够和技术受限的难题。<br />
<br />
多西、艾未未和科技博客ReadWriteWeb创始人麦克马努斯(Richard MacManus)参加的是一场在佩利媒体中心(Paley Center for Media)举行的关于数字化积极运动的讨论。来自世界各地的人们也通过Twitter参加了讨论，其发言在与会者背后的一块大屏幕上显示。<br />
<br />
在这场讨论之前两天，报道称，由于围绕审查问题的谈判陷入僵局，谷歌(Google)“99.9%肯定”将关闭它在中国的搜索引擎。在中文搜索市场，谷歌占据约35%的份额。与会人士赞扬谷歌的决定，称它有勇气，鼓舞人心。<br />
<br />
艾未未表示，他希望Twitter有中文翻译，以便有能力翻越防火 的用户能够阅读Twitter上的发言。<br />
<br />
Twitter创始于2006年，上面的每条信息不超过140字，已经成为数字化积极运动的一个工具。艾未未曾用它来探求四川地震中有多少学生死亡。<br />
<br />
当被问到他是否会把用户信息提供给中国政府时，多西表示，他希望公司能够与美国政府合作，以确保此事不会发生。<br />
<br />
多西说，第一步是翻译，让网站能够用中文访问，这是公司真正努力要做的。<br />
<br />
但他说，进入中国是一件“非常难做”的事情。<br />
<br />
本文译自美联社(Associated Press)</div>

							
							
							
							
