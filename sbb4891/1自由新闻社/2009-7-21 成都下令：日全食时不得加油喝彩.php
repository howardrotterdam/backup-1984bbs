																											<div id="postmessage_170927" class="t_msgfont">成都启动应急方案应对日全食 加油加气暂停1小时 <br />
<br />
2009年07月21日 08:09:14 　来源：四川新闻网<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;成都是我国在日全食带中首个经过的特大中心城市，为应对日全食期间能见度下降、气温降低和湿度上升，以及对交通运输、生产作业、通信安全和社会治安等带来的一定影响，7月20日，成都市政府办公厅发出关于妥善做好应对日全食工作的通知，在包括市内加油气站暂停营业一小时等方面提出了十一项应对措施。 <br />
通知指出，此次日全食持续时间为三分二十三秒。由于日全食期间能见度下降、气温降低和湿度上升，会对交通运输、生产作业、通信安全、社会治安等带来一定影响，也可能在部分人群中产生迷信猜测和心理恐慌。因此，成都市政府办公厅要求各级各部门要针对日全食对人民群众生产生活可能带来的影响，按照职责分工，积极做好应对准备工作，切实保障公共安全，维护全市社会稳定。<br />
<br />
&nbsp; &nbsp; 通知要求，成都市公安局和市交委要及早做好城乡交通应急部署，加强对驾驶人员的教育管理，防止重特大交通事故的发生，保障日全食期间交通顺畅和市民出行安全；市信息办要组织协调各电信运营商做好应急通讯准备，采取有效措施化解日全食期间可能出现的通讯高峰拥堵现象，确保通信畅通；市能源办要协调电力、燃气企业做好电力、天然气中断应急准备，7月22日早上9点至10点市内各加油站、加气站暂停营业。市水务局要组织协调自来水公司做好自来水供应保障，确保正常的生产生活秩序。<br />
<br />
&nbsp; &nbsp; 通知要求，7月22日9点至10点将停止一切城市高空作业和建筑施工，如确需施工生产的须按夜间施工生产相关规定执行。严格控制机关、企事业单位、居民院落楼顶出现的观测活动，防止拥挤或坠落事故发生。 <br />
<br />
通知还对开启城市照明设施、做好医疗救治、加强旅游组织管理、严格消费市场监管、加强天气监测预警等工作提出了要求。根据安排，7月22日上午成都市内高空游乐设施暂停开放，同时严禁各种商业促销活动，严格管理流动商贩并严防哄抬物价。<br />
<br />
&nbsp; &nbsp; 为加强社会治安管理，7月22日上午严禁举行大型户外活动，公安机关将对人员密集场所及金融机构、大型商场等重点防范部位有针对性地部署警力，严厉打击各类破坏活动，对造谣惑众、影响社会稳定的行为，公安机关将依法迅速果断处置。<br />
<br />
&nbsp; &nbsp; 为维护日全食期间正常的交通秩序，成都全市交警将在7月22日上午全警上路维护城区道路的交通顺畅，日全食期间成都城市照明则将启动夜间照明模式。(记者 蒋亮)<br />
