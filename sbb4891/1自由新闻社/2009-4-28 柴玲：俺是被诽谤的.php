年六三六五事件前学运领袖柴玲控告纪录片《天安门》(The Gate of Heavenly Peace)的制作公司侵害其声誉及公司商誉再引起争议，近百的学者联名声援言论自由。也有前学运领袖表示理解，至于柴玲用法律的手段维护自己的权益，与过去的历史切割外界有不同看法。（何山报道）<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;柴玲2007年5月在美国波士顿入禀法院，以诽谤和侵犯商誉，起诉描述 “六三六五天安门事件”的纪录片《天安门》的制作公司（the Long Bow Group），再次引公众的关注。过百名来自美国、英国、欧洲的中外学者，在网上发表联署声明，为《天安门》的作者撑腰，当中包括来自北京的作家王力雄及人民大学的教授。 (博讯 boxun.com)<br />
<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 当年六三六五事件参与者，入狱超过10年的北大学哲学系研究生李海，周一晚对本台表示，他不清楚事件，但认为当年的参与者透过法律的手段，与20年的事件作出切割，是个人的选择，他对柴玲表示理解。他说：“柴玲现在这样做，去保护她的利益，我是可以理解的。但这个利益是否正当?我无从判断。正如那个作者，有他的权利，创作和表达自己的意见。她有自己的利益所在，她想办法保护自己的利益，可以理解。对柴玲我还有这样一个想法，我觉得她当年作得很出色，这里面有一些小的是非，我也听说过一些，但是当时的运动，她起的作用很大，我想不能要求她，一个人以后一直是这样，或者永远是这样。她只要当时作用应该做的事，后来她退出，完全是正常的事，她不退出，更好。”<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 不过，联署反对柴玲控告的英国华威大学学者陈美红对本台表示，六三六五是一个集体回忆，提及柴玲及其公司的，不止纪录片《天安门》的制作者，她不能认同柴玲的指控。她说：“我们这些老百姓很难会忘记，也有人选择一定不要忘记，当年这些学生领袖给我们一些启蒙。我觉得的柴玲的这个诉讼，当然她的选择，她的公司是觉得是需要受到保护，那我能够明白，但同时我又觉得，这家被起诉的民间团体是一个学术研究团体，他们也是出于关怀中国，而拍了那套电影《天安门》。我觉得从一个历史的纪录来看，他们做得很有价值，所以他们这样被柴玲告，我觉得是很遗憾的事。”<br />
&nbsp; &nbsp; <br />
