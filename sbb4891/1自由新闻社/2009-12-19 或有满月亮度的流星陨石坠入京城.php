爆闪<br />
<br />
“现在北京越靠西边的目击者提供的信息越重要。”一颗亮度堪比满月的火流星从16日的夜空中划过，而这只有短短几秒钟的瞬间被天文馆的监控探头捕捉到。昨天，天文馆专家表示，伴随着这颗火流星的划落，可能会有陨石坠落在北京，为确定陨石坠落具体位置，北京天文馆欲寻目击证人。<br />
<br />
　　天外来客划过夜空<br />
<br />
　　火流星是较大的流星体陨落时产生的流星现象，这种流星体在稠密的地球低层大气内高速运行时，发出耀眼的光芒，是天空中最令人惊艳的天文现象之一。16日夜里，一名北京天文馆的工作人员在经过健翔桥时，不经意抬了一下头，看到在更高的匝道桥背景的夜空上，有一个很亮的点在划动，并在朝西落时爆闪了一下。<br />
<br />
　　“有满月的亮度吧。”这名工作人员表示，这是一颗并不常见的火流星，当时的时间为夜里10时23分。第二天，他来到单位，经查看天文馆的监控探头，意外发现这短到几秒钟的瞬间正好被探头拍摄下来。<br />
<br />
　　或有陨石降落京城<br />
<br />
　　昨天，记者在拍摄到“火流星”划过的监控视频中看到，在探头监控的远景建筑中的左侧为北京天文馆老馆，这时漆黑夜幕中突然出现了一个亮点，划过夜空，越来越亮，并在经过老馆圆顶上方的瞬间发出了像爆炸的火光一样的光芒，随后即被屋顶遮挡住，这一过程持续仅约3秒钟。<br />
<br />
　　“你在视频看到的好像是在老馆上方划落，但实际上划落的地方与天文馆要有一二百公里的距离。”北京天文馆馆长朱进表示，这颗火流星如果在坠落地面时没有燃烧殆尽，就有可能有陨石降落到北京地区。北京天文馆算是比较大的陨石收藏单位，但是在几百块的陨石馆藏里，还没有从北京地区发现的陨石。<br />
<br />
　　面向公众寻目击者<br />
<br />
　　据介绍，火流星划落当天，一位天津的天文爱好者也目击了这一景观，这说明这颗火流星很可能就坠落在北京郊区一带。相关专家表示，一颗3年前在黑龙江地区发现的火流星残骸重约1000克。据预测，如果能找到16日的陨石，可能比这颗的体积要大。<br />
<br />
　　朱进表示，目前已有部分市民致电天文馆，表示目击到16日火流星划落，但都在市区，与天文馆监测到的情况差不多。而根据观测，这颗火流星很可能坠落到北京西边与河北交界处，估计在百花山一带还要往西，因此，住在北京越靠西的市民观测到火流星坠落的，提供的信息越重要。同时，北京所有朝西拍摄的监控探头有可能也捕捉到当时的画面，具体时间为16日22时23分，如果能得到更多的视频或照片资料，对于找到陨石将有很大帮助。晨报记者 康健</div>

							
							
							
							
