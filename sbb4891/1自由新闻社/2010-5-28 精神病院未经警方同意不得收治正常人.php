													<h2>公安部规定精神病院无警方同意不得收治正常人</h2>
						
						
																											<div id="postmessage_644509" class="t_msgfont"><a href="http://news.sohu.com/20100528/n272398121.shtml" target="_blank">http://news.sohu.com/20100528/n272398121.shtml</a><br />
2010年05月28日07:48<br />
<br />
　　来源：中国警察网<br />
<br />
&nbsp; &nbsp; 本报武汉5月27日电 记者秦千桥报道：5月26日至27日，公安部在湖北武汉召开全国安康医院工作会议。会议提出，有条件的安康医院可以围绕“事前干预、事中处置、事后收治、后续照管”开展工作。<br />
<br />
　　据了解，1998年至今，全国安康医院累计收治肇事肇祸精神病人4万余人次，其中严重肇事肇祸的占30%。一些安康医院深入社区和企事业单位，配合公安机关做好社区防治、监控和收治工作，达到了“收治一个安宁一家，收治一个造福一方”的社会效果。会议提出，安康医院要在社会管控中发挥更重要的作用，要密切配合好治安、派出所、刑侦等部门的工作，有条件的安康医院可以围绕“事前干预、事中处置、事后收治、后续照管”开展工作。<br />
<br />
　　据介绍，“事前干预”主要是指导社区掌握重症精神病人信息，及时了解情况，督促家属履行监护职责，防止发生肇事肇祸事件发生；“事中处置”主要是协助指导办案部门利用精神疾病专业知识，对正在肇事肇祸的精神病人进行处置和制服；“事后收治”是安康医院的主要任务，对发生肇事肇祸行为的精神病人进行收治；“后续照管”要求安康医院要对出院精神病人进行跟踪照管，指导家属和社区的监护、管控工作，切实防止精神疾病的复发和再次肇事肇祸行为的发生。<br />
<br />
　　会议强调，安康医院内要建立监督机制，坚决防止司法鉴定中的徇私舞弊行为，在收治精神病人中要特别注意收治程序和收治对象的把关，没有公安机关办案部门的审核同意，对不是精神病患者的人员，一律不得接收。<br />
<br />
&nbsp; &nbsp; 各地普查精神病人，是值得赞扬推广的：<br />
<br />
&nbsp; &nbsp; 湖北将评估精神病人 对可能造成危害者免费治疗<br />
<br />
&nbsp; &nbsp; 22日，我省召开“公共安全与精神卫生应对”学术讲座。会上，省卫生厅精神卫生中心介绍，将很快开展对全省80万重型精神病患者的调查评估，加强治疗管理，对可能危害他人、社会的患者给予免费治疗。[详细]<br />
<br />
&nbsp; &nbsp; 黑龙江将对4000余名重症精神病人筛查监控<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;黑龙江省今年继续在哈尔滨、大庆、牡丹江、鸡西四个市开展对重性精神疾病患者的管理和治疗。<br />
<br />
　　据介绍，黑龙江省将从这四个市的620万覆盖人口中筛选与登记出重性精神病人4000余人，对他们进行专业的精神科诊断复核及危险行为评估，对其中有危险行为倾向的贫困患者提供免费药物治疗及至少每年四次的相关化验检查；并提供有专科医生进行半年一次的服药后免费疗效评估与治疗方案调整；还给与每年两次的免费复诊。[详细]<br />
<br />
&nbsp; &nbsp; 亚运期间严管高危精神病人 广州已展开全市普查<br />
<br />
&nbsp; &nbsp; 据悉，目前广州共有4万多登记在册的重症精神病人，其中有肇事肇祸倾向的近千人。亚运期间，将由精神病医院、社区街道干部、社区民警、家属的四方联动机制将对这类病人实施重点管控。[详细]<br />
<br />
&nbsp; &nbsp; 福建警方排查精神病人 对风险度极高者强制治疗<br />
<br />
&nbsp; &nbsp; 福建省公安厅要求，全省各地认真开展重性精神病人摸排核实工作。对摸排出的重性精神病人，积极协同相关部门动员、督促送医治疗；对流散在社会上的重性精神病人，要商请民政、卫生等部门，加强救助、收治工作。[详细]<br />
<br />
&nbsp; &nbsp; 但我们也要明白，关怀照顾精神病人是政府应尽的责任：<br />
<br />
&nbsp; &nbsp; 学者称化解社会矛盾可从关注精神病人始<br />
<br />
&nbsp; &nbsp; 我国官方公布的重症精神病人的数量为1600万人，但这个数字还是1992年调查得出的，后来就没有全国性的数据了（央视国际网2005年8月26日）。<br />
<br />
　　除了那些原为国有企事业单位干部、职工后来患精神病的人之外，其他的精神病人治病、生活、看护的费用几乎完全落在他的家人身上。家里有一个精神病人，其父母、配偶、成年子女几乎无不倾家荡产很快陷入赤贫。生计难以为继的家人往往被迫终止精神病人的治疗，放松对于精神病人的看护。至于没有父母、配偶、成年子女的精神病人，则大多在外面流浪，风餐露宿，死无葬身之地。[详细]<br />
<br />
&nbsp; &nbsp; 政府不能任由精神病人散落民间 <br />
<br />
&nbsp; &nbsp; 很多症状轻微的精神病人本该接受医疗，但由于医疗资源紧张或担心承受社会偏见，而得不到及时、有效的治疗，以至于恶化。截止2009年9月,我国注册精神科医师仅16383人,平均每10万人仅有1．26名精神科医师，全国地市级以上精神病医院不足千家，心理医生缺口90％，全国至少有5600万各类精神障碍患者尚未接受过任何有关的医疗，即使是严重的精神疾病患者，每4人中也仅有1人接受过正规的精神科医疗服务。<br />
<br />
　　因此，政府应加大精神病治疗机构和医护队伍的建设；应加大宣传，营造社会关爱氛围，切实地让精神病人早发现早治疗。<br />
<br />
　　如果说被精神病人伤害是“天灾”，那任由精神病人散落民间就是“人祸”。精神病人这枚“不定时炸弹”，还要靠政府来拆除“引信”。[详细]<br />
<br />
&nbsp; &nbsp; 最后，请注意区分谁是真正的精神病人：<br />
<br />
&nbsp; &nbsp; 近30名十堰市原五交化有限公司下岗职工，从报纸上得知，省里下来的巡视小组住在宏正酒店，便于4月9 日上午聚集在宏正酒店门口，打出横幅反映公司改制国资流失和原单位领导涉嫌腐败问题。<br />
<br />
　　职工王忠梅介绍说，30分钟后，当地警察出动，带走10名职工，另有十堰市民彭宝泉和邓复华也被带走。王忠梅说，彭邓两人当天在现场拍了几张照片。<br />
<br />
　　次日凌晨，邓彭被送进东风茅箭医院精神科。十堰警方的解释是，彭、邓为上访者写材料，然后参与策划聚集事件，并收取上访者万元交通费，为判明两人是否为发病期，故将其送往精神病院检查。<br />
