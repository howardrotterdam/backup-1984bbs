																											<div id="postmessage_644065" class="t_msgfont">本报武汉5月27日电 记者秦千桥报道：5月26日至27日，公安部在湖北武汉召开全国安康医院工作会议。会议提出，有条件的安康医院可以围绕“事前干预、事中处置、事后收治、后续照管”开展工作。<br />
<br />
据了解，1998年至今，全国安康医院累计收治肇事肇祸精神病人4万余人次，其中严重肇事肇祸的占30%。一些安康医院深入社区和企事业单位，配合公安机关做好社区防治、监控和收治工作，达到了“收治一个安宁一家，收治一个造福一方”的社会效果。会议提出，安康医院要在社会管控中发挥更重要的作用，要密切配合好治安、派出所、刑侦等部门的工作，有条件的安康医院可以围绕“事前干预、事中处置、事后收治、后续照管”开展工作。<br />
<br />
据介绍，“事前干预”主要是指导社区掌握重症精神病人信息，及时了解情况，督促家属履行监护职责，防止发生肇事肇祸事件发生；“事中处置”主要是协助指导办案部门利用精神疾病专业知识，对正在肇事肇祸的精神病人进行处置和制服；“事后收治”是安康医院的主要任务，对发生肇事肇祸行为的精神病人进行收治；“后续照管”要求安康医院要对出院精神病人进行跟踪照管，指导家属和社区的监护、管控工作，切实防止精神疾病的复发和再次肇事肇祸行为的发生。<br />
<br />
会议强调，安康医院内要建立监督机制，坚决防止司法鉴定中的徇私舞弊行为，在收治精神病人中要特别注意收治程序和收治对象的把关，没有公安机关办案部门的审核同意，对不是精神病患者的人员，一律不得接收。<br />
<br />
<br />
