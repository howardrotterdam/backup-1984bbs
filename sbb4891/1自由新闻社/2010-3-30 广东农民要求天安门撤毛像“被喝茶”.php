 <br />
70多岁的广东农民黄廷楷，去年12月30号发表了一篇名为“我强烈要求毛泽东的画像要从天安门城楼上拆下来”的文章，在因特网上被广泛转载。文章说，“至今还将毛泽东的画像挂在天安门城楼上，我就有意见了，因为那不是个人行为，它代表国家形象，影响全国人民。”<br />
 <br />
黄廷楷对记者说明他对毛泽东的观感：“我现在70几岁了，所谓解放60年，所谓中华人民共和国，我是看着它长大的，它的所作所为，毛泽东的所作所为，我都看得清清楚楚。看着毛泽东的所作所为，我很反感。小时候，我看他斗争地主呀，那么残酷，那么不得民心，我就很反感。慢慢慢慢的，又经过文化大革命呀，他做了很多很多不人道的事情。”<br />
 <br />
黄廷楷说：“1957年‘反右斗争’是不是在毛泽东领导下搞的？是不是害了几十万中国知识分子？是不是害了超过百万人？三面红旗是不是毛泽东一手举起来的？1958大跃进是不是他要搞的？1959年庐山会议上反右倾是不是他搞的？由此而引发饿死几千万农民是不是他的责任？文化大革命是不是毛泽东发动的？是不是给全中国造成10年浩劫？”<br />
 <br />
黄廷楷说明他要求撤除毛像的理由：“都是文革后遗症。现在那么多不好，那么的胡作非为，老百姓没办法享受自由，没有享民主权力，所以我就很反感，所以我就认为，毛泽东像要拿下来，那么污浊的东西要洗掉，那些东西要扫掉。”<br />
 <br />
*公安局出面，黄廷楷“被喝茶”*<br />
 <br />
黄廷楷表示，他受到来自官方的压力：“去年12月30号我上网，2月28号公安局打电话给我，请我喝茶，要同我交朋友。因为我28号在忙，29号他们请我到贵州大酒楼喝茶。”<br />
 <br />
黄廷楷讲述了他“被喝茶”的内容：“他就说毛泽东呢，是神来的，怎么怎么样。我说你们是共产党，不是无神论吗，你们怎信神呢？他们就不好答。最后呢，我说你们拿出证据证明毛泽东是神，我就觉得神是造福人间的，我就没有听说过神是作祸人间的。那毛泽东作了那么多坏事，是不是，作了那么多罪恶，他这样算什么神呢？”<br />
 <br />
黄廷楷并且质疑，“像毛泽东这样的人的画像都可以继续挂在天安门城楼上，孙中山的画像为什么又不能挂上去？”</div>

							
							
							
							
