　　“为了发展壮大我党的武装力量，在战后夺取全国政权。我们党必须严格遵循的方针是‘一分抗日，二分敷衍，七分发展，十分宣传’。任何人，任何组织都不得违背这个总体方针。”！<br />
<br />
　　一九六四年七月十日，日本社会党委员长佐佐木更三偕委员黑田寿男去北京，与毛泽东有下面一段对话：毛：我曾经跟日本朋友谈过。他们说，很对不起，日本皇军侵略了中国。我说：不！没有你们皇军侵略大半个中国，中国人民就不能团结起来对付蒋介石，中国共产党就夺取不了政权。所以，日本皇军是我们中国共产党人的好教员，也可以说是大恩人，大救星。<br />
<br />
　　佐佐木：今天听了毛主席非常宽宏大量的说话。过去，日本军国主义侵略中国，给你们带来了很大的损害，我们大家感到很抱歉。<br />
<br />
　　毛：没有什么抱歉。日本军国主义给中国带来了很大的利益，使中国人民夺取了政权。没有你们的皇军，我们不可能夺取政权。这一点，我和你们有不同的意见，我们两个人有矛盾。（众笑，会场活跃）。<br />
<br />
　　佐佐木：谢谢。<br />
<br />
　　毛：不要讲过去那一套了。日本的侵略也可以说是好事，帮了我们的大忙。请看，中国人民夺取了政权，同时，你们的垄断资本、军国主义也帮了我们的忙。日本人民成百万、成千万地醒觉起来。包括在中国打仗的一部份将军，他们现在变成我们的朋友了。<br />
<br />
　　摘自《毛泽东思想万岁》，第五三三至五三四页。）<br />
<br />
