<br />
1991年国务院修订颁布的新办法中沿用了这一修改后的纪念日。1999年，中华人民共和国国务院令第270号《全国年节及纪念日放假办法》(1949年12月23日政务院发布，1999年9月18日国务院修订发布)中发布的有关抗日的纪念日有“七七”抗战纪念日、“九三”抗战胜利纪念日、“九一八”纪念日。这是中国政府以法规的形式确定了抗日战争纪念日。<br />
