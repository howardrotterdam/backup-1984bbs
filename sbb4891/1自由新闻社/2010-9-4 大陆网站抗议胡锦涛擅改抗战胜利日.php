																											<div id="postmessage_812812" class="t_msgfont">来源：参与 作者：小熊 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;（参与2010年9月4日讯）：昨天（2010年9月3日）10时，胡锦涛率八常委前往北京卢沟桥畔的宛平城参观中国人民抗日战争纪念馆，向抗日战争烈士敬献花篮，并宣布“今天是中国人民抗日战争胜利65年周年纪念日”后，立即引发全国百姓的质疑和抗议：从来都是8月15日是抗战胜利纪念日，胡锦涛今年擅自改为9月3日，是何居心？ (博讯 boxun.com)<br />
<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; “博客中国”网站发表晓军题为《最俱辉煌的纪念日面对谎言与冷漠》的博文说――<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 从1949年到现在，中国人都知道8月15日是日本当年的投降日，也是中国抗战胜利纪念日，几十年来，上至中央，下至百姓，都是在这一天举办纪念活动。但今年的八月十五日却被莫名其妙的改变为舟曲灾难全国哀悼日！就在全国民众为此迷惑茫然之际，9月2日傍晚，中央电视台突然宣布9月3日为抗战纪念日！次日人民日报发表的纪念社论，谎话连篇，完全是几十年来一贯的八股腔调，只字未提国军几百万将士悲壮惨烈，牺牲巨大的历史实事，整个社论的基调是，抗战胜利完全是中国共产党的功勋。面对着已经真相大白的历史实事，最高当局依然在睁眼说瞎话，依然在欲盖弥彰，歪曲历史。一派胡言，竟然成为新闻向全国播放。我相信，今年可以应付过去，往后几年也可以应付过去，应付的越多则欠债越多，欠债还钱是个硬道理，总有一天，这个巨大的欠债，终将要以惨重的代价偿还。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 面对百姓的群起质疑与抗议， 深圳卫视等地方传媒不得不以发表《你认为应当如何纪念》的“调查问卷”变相解释：1945年8月 15日，是日本宣布投降日；但1945年9月2日，日本政府代表才在投降书上签字（签字仪式在美国“密苏里”号巡洋舰上），徐永昌代表中国政府在日本投降书上签字确认；第二天，也就是9月3日，当时的国民政府下令举国庆祝3天，并从第二年开始以每年9月3日为抗日战争胜利纪念日。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 专家认为这一解释可疑：这一说法似乎表明，当年国民党领导的中国政府从来就是以9月3日为抗日战争胜利纪念日，是中共在1949年后否定了9月3日，而改定8月 15日。既如此，人民日报的纪念社论应该对此作说明，胡锦涛应该代表中共向全国人民道歉：以前中共错定了抗战胜利纪念日。如此之后，百姓才有可能接受新抗日战争胜利纪念日。何以中央不作任何解释，只让地方媒体作一些没有权威的说明？<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 而凯迪网昨天则有帖子说：“九月三号是台湾的军人节，为感念因保护家园牺牲生命的军人，台湾领导人马英九与行政院长吴敦义上午赴忠烈祠，参加一年一度的祭祀典礼，向军公职殉职人员致意：台湾为表扬对日抗战牺牲生命者，从一九五五年设立九三军人节”――这一信息若准确，说明国民党领导的台湾，也只是设立九三军人节，并未将9月3日定为抗日战争胜利纪念日。这就让人怀疑，胡锦涛擅自将抗战胜利纪念日由8月15日改为9月3日，只是为了与台湾统一？纯属一个统战的把戏？！<br />
