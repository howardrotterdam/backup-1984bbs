													<h2>关于“打人总是不对的”</h2>
						
						
																											<div id="postmessage_713561" class="t_msgfont">有位能把“打人是不对的”理解成“骂人是对的”，我还能说什么呢？<br />
<br />
这一点没什么意思争辩下去。我只说说为什么我说“打人总是不对的”：无煽情，不新闻，已经成了一个不成文的套路。一个社会底层的人被人打了，打人者还是有车开的有钱人，特别是，起因还是因为被打者无意中妨碍了有钱人用包子喂狗。<br />
<br />
这是多么好的新闻素材。但，整个故事就没有可质疑的地方吗？<br />
<br />
为了一点小事，一个人把另一个人打成重伤，原因除了“不拿穷人当人看”这个新闻一直在暗示的原因外，就没有别的原因？<br />
<br />
新闻毕竟不同于司法调查，作者往往带有强烈的主观观点，有意或无意地忽略一些事实。这对我们已经不是陌生的事了。从这个新闻里，我看不到事情真正的起因，作者只是含糊带过，只从一方了解事情经过，都没有采访到打人者。<br />
<br />
但我看到的唯一事实就是，开车人把清洁工打伤，双方究竟谁先动的手读者根本无法从这个新闻中获知。<br />
基于这个事实，我自己的评论是：打人总是不对的。这句话背后的含义是：即使清洁工也有责任，甚至是很大的责任，那开车人也不应该把人打伤。</div>

							
							
							
							
