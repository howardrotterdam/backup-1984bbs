 <a href="http://cd.qq.com/a/20100630/002083.htm" target="_blank">http://cd.qq.com/a/20100630/002083.htm</a><br />
重庆晨报 （记者 袁婧）昨日清晨6点多，从四川农村来重庆主城打工的一位环卫工人，因清扫了渝中区大黄路半岛深蓝小区门前一个包子，被打得左眼严重血肿如“包子”，手指骨折，全身是血。打人者是一位30多岁的男子，据他称，包子是他刚从附近买来喂狗的。事发后，大坪派出所介入协调处理此事。<br />
看见地上有包子&nbsp;&nbsp;环卫工连忙扫开<br />
半年前，56岁的苏丕先从四川农村来到重庆主城与小儿子一家同住。为了补贴家用，他进入渝中区环卫所一环卫公司做清洁工，主要负责大黄路附近的清洁。<br />
“4点半出门，天刚刚亮时扫到半岛深蓝小区门口。”苏丕先说道。此时，他仍穿着血迹斑斑的工作服，脸上残留不少血迹，左侧太阳穴附近包着白色纱布，由于四周严重血肿，左眼已眯成了一条缝。他告诉记者，昨日清早，他像往常一样很早就起床，4：30左右出门工作。<br />
天刚蒙蒙亮时，苏丕先扫到了半岛深蓝小区大门，见旁边一辆黑色本田轿车附近有个白色的东西。仔细一看，发现是个包子。由于包子摆在地上，苏丕先估计没人要了，就一扫把扫开。<br />
刚扫了一扫把&nbsp;&nbsp;车上男子燃起怒火<br />
“你有神经病啊？没看到我在喂狗哇？”苏丕先说，他刚扫完一扫把，本田轿车上的男子就朝他大声嚷道，边说着边走下车，面对面地骂。<br />
苏丕先说，见自己的一扫把点起了火，他赶紧赔礼道歉，称自己不是故意的，以为那是别人不要的包子，他接连道了四次歉，并表示愿意买一个包子赔，然而，男子不但没下火，反而动手打人。<br />
“拳头可能不够长，就到车上拿了一根木棍。”苏丕先低声说道，男子动手打了几拳，便折回车上拿出一根大约40厘米长的方形木棍，打向其头部，整个被打过程中，他没有还一次手，只在最初时用扫把挡了一下。<br />
待本田男子停手后，苏丕先拿出电话，拨打了110报警。随后，大坪派出所介入调查，并协调处理此事。<br />
害怕家人担心&nbsp;&nbsp;不敢通知他们<br />
被打后，苏丕先头部多处冒血，整套工作服前面部分被溅得血迹斑斑，脸上一片模糊。他很快被送至大坪医院急诊科。经检查，其头部多处血肿，并伴有手指骨折。<br />
“我们来的时候，他已经在医院了，快8点钟了。”苏丕先的妻子官女士，两眼泪光闪闪地说。她称，由于丈夫害怕家人担心，事发后一直未打电话通知他们，一家人早上7点多钟时才从环卫公司处得知此事。<br />
妻子官女士头发花白，她表示，丈夫平日性格内向，言语不多，来重庆主城半年多还从未和人吵过架。此外，苏丕先供职的环卫公司一负责人也透露，平日工作中，只要是领导的安排，苏丕先都点头答应，“话不多肯做事”。<br />
当记者问及是否看到狗儿时，苏丕先表示，轿车车门一直半开着，附近确实有只狗儿，但离包子有段距离。</div>

							
							
							
							
