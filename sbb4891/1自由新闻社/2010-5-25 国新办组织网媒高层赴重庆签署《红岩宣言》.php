																											<div id="postmessage_638146" class="t_msgfont">国内知名网媒高层齐聚重庆体验&quot;红色之旅&quot;<br />
<a href="http://news.163.com/10/0525/08/67H3PPED0001124J.html" target="_blank">http://news.163.com/10/0525/08/67H3PPED0001124J.html</a><br />
<br />
2010-05-25 08:57:19　来源: 华龙网(重庆)<br />
<br />
核心提示：昨日,参加“第七届北京网络媒体红色故土重庆行”的35家国内知名网络媒体高层抵渝，开始“红色之旅”。<br />
<br />
<img src="http://img1.cache.netease.com/catchpic/3/30/30DD410F7501FC29B16F1E64AD6F3A5B.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
昨日,人和小学,来自全国知名网站的CEO和小学生同唱经典红歌。 记者 张路桥 摄<br />
<br />
华龙网5月25日报道　昨日,参加“第七届北京网络媒体红色故土重庆行”的35家国内知名网络媒体高层抵渝。“文强仅仅一个厅级干部,但人们对他犯罪的关注度却比省部干部还高,说明重庆打黑除恶深得民心。”百度公司对外事务部高级经理卢彤在接受采访时称,重庆打黑除恶在网络上的关注度史无前例,据他们统计,包括新浪、搜狐、网易等几乎所有门户网站都开设专栏,报道重庆打黑除恶。<br />
<br />
“喧闹的重庆充满希望!”<br />
<br />
3年前，卢彤来过重庆，留给他的印象天空是灰蒙蒙的,而今看到的到处是热闹的工地,就像奥运会前的北京一样,给人一种热血沸腾的感觉。这说明重庆已进入一个快速发展期,喧闹的重庆充满希望!<br />
<br />
“在北京也听说重庆的‘唱读讲传’,但不知是咋回事,今天给人的感受就是这个活动开展得真好。”卢彤说,一群十余岁的孩子能完整背诵《滕王阁序》,这是很了不起的事。有人说小学6年影响一生,七八岁的孩子能讲几十甚至上百个革命故事,这些故事的积极意义都将影响到他的成长,对他们人生观、价值观的形成都会带来重要作用。“唱读讲传”会对未来重庆人的素质带来深远的影响。因此，从唱的角度来讲，重庆也是充满希望的。<br />
<br />
门户网站专栏报道重庆打黑<br />
<br />
“重庆为什么成为焦点？过去一直没有理由，而今无论是打黑除恶还是‘五个重庆’建设，都成为世人瞩目的焦点。”卢彤说，这些焦点的形成说明重庆发展步入良性循环的信号。<br />
<br />
卢彤说，改革开放30年，我们的经济社会发生了巨大的变化，随之而来的是一些人私欲的膨胀、对公权的滥用，如文强、彭长健等人，就滥用自己的职权，收受贿赂谋取私利，为黑社会充当保护伞，本来这权力是人民赋予他们的，但人民却成为了公权私用的受害者。因此，人们关注重庆打黑除恶，实质是呼唤社会的公平正义。<br />
<br />
“在网络上，群众关注打黑除恶是史无前例的。”卢彤说，重庆打黑除恶给全国提供了经验，重庆可以充分利用打黑除恶的成果，宣传“平安重庆”良好形象，扩大重庆的开放度。<br />
<br />
观点 <br />
<br />
网言网语　用摇滚演绎红歌<br />
<br />
我市的“唱读讲传”如何与互联网相结合？如何利用互联网平台传播红色经典？昨日六间房网站创始人兼CEO刘岩和新浪网监控副总裁李彤均发表了各自的看法。<br />
<br />
刘岩认为，重庆红歌传唱无论是形式还是效果都非常不错，但红歌传唱必须进行改革才能真正深入年轻人的心。六间房网站曾做过调查，周杰伦、刘若英、张信哲等人的歌曲就是旋律再差，歌词再不怎么样，可一经他们演唱便迅速在80后、90后的年轻人中传开，而一些含义深刻、旋律优美的红歌却始终得不到年轻人的青睐，没有走进他们的世界，所以我们需要改变红歌的传唱方式。如用摇滚的方式来演唱《国际歌》，让周杰伦来演唱《我爱北京天安门》同样能达到传唱红歌的目的，甚至在年轻人当中会取得更好的效果。<br />
<br />
李彤则认为，要避免就事论事、板着脸教育网民，而要善于用“网言网语”传播红色经典。利用互联网这一媒介传播红色经典，需要通过网络媒体的影响力，结合网络的特点慢慢渗透。比如，用流行的“网言网语”来传播，避免就事论事，一板一眼地教育网民。“那样就不符合互联网网民的特点了。”<br />
<br />
素质教育　希望能由点到面<br />
<br />
<br />
昨日下午，无论在参观我市谢家湾小学、人和街小学还是求精中学和重庆市规划展览馆，视频网站优酷副总裁朱辉龙都拿着一个巴掌大小的玫红色摄像终端在拍摄。他说，他将把昨天拍摄下来的内容上传到优酷网站上。看到谢家湾小学的素质教育，朱辉龙直言，希望“这样的点能真正到面，覆盖更多小学生”。<br />
<br />
北京网络媒体嘉宾们到渝后参观的第一个地方就是谢家湾小学，嘉宾们在谢家湾小学的教学楼走道，看到两旁教室里的学生们，有的在练习书法绘画，有的在下围棋、象棋，有的在朗诵讲故事，还有的在唱歌跳舞。看到这些多才多艺的孩子们，朱辉龙感叹，一直以来整个国家面对的都是应试教育，这是多少年的体系，如果所有学校都能彻底转变成这种素质教育该有多好。<br />
