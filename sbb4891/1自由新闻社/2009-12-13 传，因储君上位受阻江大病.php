(博讯北京时间2009年12月12日 首发)<br />
<br />
官场观察工作室 昭明<br />
<br />
作领导的，为了不至于陷入被动局面，经常隐瞒自己的真实意图，把“文章”交由下属去办，以观察下属的立场与忠诚度。而作下属的又要经办具体事务，一时无法绕开，这时候就要正确体会领导的真实意图，必要的时候要主动试探一下。 <br />
<br />
1978年底的中央工作会议，以及随之而来的十一届三中全会上，党内元老们基本取得共识，内定华国锋将会辞去中央军委主席与中共中央主席的职务，为了平稳过渡，正式宣布却要到1981年的十一届六中全会。到底谁来接任？叶剑英与其他老同志建议小平接任。但邓小平明确表示，自己可以接任中央军委主席的职务，但蜿蜒谢绝中共中央主席的职务，力荐较年轻的胡耀邦接任。这到底是小平同志的谦虚，还是他的确另有考虑？为此胡耀邦私下试探性地向小平同志表示，在党内老同志们中，自己还年轻，还没作出什么重大成绩，一时无法服众，由自己来当中共中央主席不合适，还是小平同志当较好。邓小平当即表示，自己不会当，如果胡耀邦也坚辞这个职务，一旦落入他人（如陈云）手里，就会影响到党的改革开放大计。所以邓小平要求胡耀邦，在中共中央主席的职务上，要“当仁不让”。所以1981年的十一届六中全会，胡耀邦顺利接替华国锋，出任中共中央主席。<br />
<br />
时间飞逝，转瞬间到了2009年，胡锦涛会否按照十七大之前与江泽民、曾庆红等人达成的政治交易那样让习近平接班？在党的历史上，写进党章里的接班人，照样可以阴沟里翻船，性命不保，被批判成阴谋家、野心家，更不要说是白纸黑字，或是空口无凭了，胡锦涛完全可以不认帐。现在人们都知道了，习近平能否成功接班的关键在于能否接班军委，而成功接班军委的关键在于能否在十七届四中全会接班军委副主席。为此，四中全会前，习近平也试图向当年胡耀邦试探邓小平那样试探胡锦涛。习表示他本人到中央工作rg不L，S多情r^槟吧，做好分工鹊墓ぷ餍枰花很大精力，自己尚不具涑新工作的能力和l件。而胡锦涛似乎早就先一步洞察到习近平的这一举动，当即向习表示：请习近平同志安心工作，关于中央的人事安排，还是交由中央来讨论，总之不要有思想压力，个人服从中央的决定就是了。<br />
<br />
从语言学的技巧上看，胡锦涛的回答实在是巧妙，一是让习近平碰了个不硬不软的钉子，意思是不要试图从我嘴里捞到半点口实，同意与不同意都是中央说了算，你我嘴里过去讲的、现在讲的都不算数；二是胡锦涛以一个虚词“中央”，就把习近平打发了。要真是同意习进军委，上海帮与太子党就会卷土重来，为陈良宇翻案，到时候就是胡派团队人头落地。若真是明确表示，同意习近平不进军委的提议，就会落下口实，江、曾就会找上门来，指着鼻子大骂胡锦涛出尔反尔。相反，若是中央的决定，大家都得服从。到底谁是中央？是中央全会吗，还是第三代核心？抑或是某个控制了北京军区与中央警卫局武装力量的军委主席？“中央”里面的学问大了，你习近平想试探虚实，逼军委主席表态，想让胡锦涛落下口实，你自己琢磨吧！<br />
<br />
四中全会前，当曾庆红在第一时间得知胡锦涛对习近平试探的答复时，气得大骂胡锦涛耍阴谋、出尔反尔，不能任凭胡锦涛在中央胡来下去，中央的政治游戏一定要有规则，如果说以前没有什么规则的话，现在则一定要给胡锦涛制定游戏新规则。江泽民得知胡锦涛的反应后明确表示，六十大庆，一不允许胡锦涛使用“第四代”的名义，二不允许胡锦涛使用“核心”的称谓。总之，要抢尽胡锦涛的风头，绝不能给胡锦涛称霸全党的机会，要让全党全军知道江泽民仍然是核心，仍然可以检阅指挥军队。<br />
<br />
在对江、曾的上海帮与太子党问题上，胡派团队一致认为，这是一场你死我活的政治斗争，任何政治交易都是暂时的，其目的都是麻痹对手，暗中调整力量对比，时机成熟时再给对手致命一击。根本不存在侥幸的、长久的政治交易，根本不存在胡派团队与上海帮、太子党和平共存的任何可能性。因为经济、政治利益，与人对权力的本性决定了这场政治斗争的不可调和性。但在语言上，为了麻痹对手，让对手心存侥幸，可以另有一套。<br />
<br />
但胡派团队具体到六十大庆的战术安排上却在内部发生了分歧，强硬派认为2009年的中共建政六十周年，是胡主席树立中共第四代核心的最后的时机，因此决不能同意江泽民与胡主席站在一起阅兵抢风头。更何况刚刚在四中全会上给了对手一击，成功阻止了习近平接班军委的企图，现在则是给习的后台老板江泽民一击的时候。阅兵本身就是现任军委主席份内的职责，与前任无关，完全可以以名不正言不顺为理由拒绝江泽民的无礼要求。<br />
<br />
