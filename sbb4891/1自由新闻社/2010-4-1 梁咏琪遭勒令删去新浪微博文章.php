艺人梁咏琪将声援赵连海的博客内容删除<br />
 <br />
香港著名歌手及演员梁咏琪，昨日（3月31日）将一篇涉及敏感话题的文章从自己的新浪博客中删除。这突显出中国对于网络的严密控制，已引起公众人物的恐惧。<br />
 <br />
梁咏琪的经纪人在与美联社电话访谈中表示，梁咏琪在其个人的新浪微博客中引用了一段关于赵连海受审的消息。她在文中声援赵连海，但随即删除了相关文字，稍后，她又再度上网留言，表示不愿见到不公平的事再度发生。<br />
<br />
但与此同时也有报道称，梁咏琪是在自己的博客上转贴有关赵连海的新闻后，“为免引起大家不安才删掉文章，并非有关部门要求。”<br />
<br />
2008年，中国爆发毒奶粉事件，30万名孩童因饮用含有三聚氰胺的奶粉而罹患肾结石，有6名病重死亡。身为受害孩童之一的家属，赵连海创立了&quot;结石宝宝之家&quot;网站，希望受害者能得到合理赔偿，并集结受害者家属力量，引起社会大众对此事件后续关注。在毒奶粉厂商&quot;三鹿&quot;刑事案犯被判处死刑后，赵连海却也因&quot;寻衅滋事&quot;罪名，于2009年底遭到搜查和拘捕。 <br />
<br />
3月30日上午，赵连海案在北京开始不公开审理，北京市公安局大兴分局的起诉意见书中指出，赵连海纠集群众并扰乱社会秩序。据悉，赵连海及其律师在庭上作出了无罪辩护。此案已引起媒体及网友的注目与讨论，但许多相关的报道，都在网上发布后不久遭到删除。美联社报道认为，由梁咏琪在博客上这段文字的删除，可以推测出，香港演艺人员被迫屈从于中国当局对媒体的审查制度。<br />
