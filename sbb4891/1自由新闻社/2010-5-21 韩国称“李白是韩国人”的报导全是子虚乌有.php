 <br />
<a href="http://www.dfdaily.com/node2/nod" target="_blank">http://www.dfdaily.com/node2/nod</a> ... ject1ai223532.shtml<br />
　早报讯 韩国驻华使馆日前声明称，近期一些媒体刊载的韩国教授称“诗人李白是韩国人”的消息，系不实报道。声明还指出，其他类似“中国文化被韩国”的诸多报道内容，事后经查明大部分报道内容均无事实依据。其余的报道是把某个人的观点当成韩国国民的普遍立场，扭曲事实真相并进行炒作。 <br />
　　声明指出，近期一些中国媒体刊载了以“韩国人硬称中国的文化遗产是韩国的”为内容的报道，此报道陆续通过网站广泛流传，“我们对此深感担忧，并予以关注。”声明中称，近期中国一些媒体刊登了关于韩国首尔大学历史系金秉德教授主张“中国诗人李白是韩国人”的报道。经韩方核实，韩国首尔大学根本就没有叫金秉德的教授，此外虽有国史系、东洋史系、西洋史系，但没有“历史系”这一学系。<br />
　　声明指出，还有一些媒体刊载关于“韩国首尔大学历史系朴正秀教授称‘朝鲜族发明了汉字’，并主张将汉字申请为世界文化遗产的内容”。此外还报道了“孔子、西施、道教鼻祖张道陵等是韩国人”的内容。<br />
　　经韩方确认，韩国首尔大学既没有叫朴正秀的教授，也没有人主张“朝鲜族发明了汉字”。<br />
