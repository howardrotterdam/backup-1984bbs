													<h2>韩国称“李白是韩国人”的报导全是子虚乌有</h2>
						
						
																											<div id="postmessage_628622" class="t_msgfont">昨天有媒体引述大陆媒体报导，指韩国学者考证李白其实是韩国人后代，引发网路反韩声浪。韩国驻台北代表部向首尔大学查证，根本没有这位学者，昨天下午发布新闻稿，指相关报导「子虚乌有」，希望媒体报导善尽查证之责，以免影响台韩双边友好关S。<br />
<br />
有媒体引述大陆甘肃日报报导指出，据南韩首尔大学历史系教授金秉德考证，李白是韩国人的后代；为此，他准备向联合国相关机构提出申诉，抗议中国歪曲历史，剽窃韩国文化，并申请将唐诗列为韩国的非物质文化遗产。<br />
<br />
消息见报后，在网路引发热烈讨论，韩国驻台北代表部一早就接到抗议电话，抗议韩国扭曲史实。代表部立刻联络首尔大学查证，结果人事资料显示，首尔大学根本没有金秉德这个人，也没有类似主题的文章被发表过。<br />
<br />
代表部官员说，「相关报导根本是子虚乌有。」a于过去曾发生多次类似桉例，包括陆媒刊载「韩国人说孔子、老子和释迦牟尼都是韩国人」、「韩国人宣称豆浆、中医甚至汉字都是韩国发明」、「长城是韩国人建造」等，都被台湾媒体大量引用，韩国驻台代表具良根认为，这次要在第一时间郑重澄清，以免台湾部分媒体又以讹传讹。<br />
<br />
官员表示，报导出来后，立刻被电子媒体和网路大量引用，「让台湾民众有不好的感觉，这不是我们希望的，而且报导内容完全没有根据。」<br />
<br />
据了解，驻台北代表部昨天也将处理过程回报给韩国外交部，韩国外交部将循外交管道，向中国大陆驻韩国大使馆表达关切，希望陆媒能自律。代表部昨天也将新闻稿转给我国外交部，希望外交部一起呼吁媒体，在引述报导时要善尽查证之责。</div>

							
							
							
							
