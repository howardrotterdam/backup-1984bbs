六&nbsp;&nbsp;神臂<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;神臂？什么东东？铁臂神功吗？不是的。‘铁臂神功’抵挡木棍铁棍还可以，如果抵挡歹徒的马刀、砍刀嘛――以后只好练习“独臂神功”了！神臂经过邵发明先生的实战检验验，效果非常的好！作为野山的精品，郑重推荐给大家！<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;护臂（神臂），是一头略大的半圆滚筒形，用合金制成，又轻又坚固。<br />
&nbsp; &nbsp;&nbsp; &nbsp;将略细的一端放在左手近碗处，边缘略上翘，以防砍刀砍在护臂上滑脱削到手背。将略粗的一端放在左肘部，顶部与肘尖平齐，并从外肘到肘窝斜斜切入，以利手臂屈伸，不受限制。<br />
&nbsp; &nbsp;&nbsp; &nbsp;将护臂套人左前肘时，刚好将左前臂的外侧（手背，小指侧）护住而左前臂的内侧（手心一侧）留下空余，系好绳子，绑紧即可。<br />
&nbsp; &nbsp;&nbsp; &nbsp;护臂的内侧，可以垫上一层软布，以作缓冲减震之用，以备应付铁棍猛劈。<br />
&nbsp; &nbsp;&nbsp; &nbsp;护臂的外面，可缝上一块普通布料，或制成”袖套“摸样，让外人看不出来。<br />
&nbsp; &nbsp;&nbsp; &nbsp;应用时，将左小臂外侧斜斜迎挡歹徒的铁棍、马刀、砍刀，那些武器全会滑到一边。<br />
&nbsp; &nbsp;&nbsp; &nbsp;多数人爱以右手执刀棍砍劈，多为斜线，也即攻向我的左侧上方。这时，只要举左臂斜迎，则可保无恙。<br />
&nbsp; &nbsp;&nbsp; &nbsp;其要，在于一个”斜“字。臂要斜，”斜“才会”滑“；身要斜，”斜“则利于右手及时反击。<br />
&nbsp; &nbsp;&nbsp; &nbsp;左臂（护臂）迎截时，尽量不要迎截歹徒的棍头，刀头处，要朝其近手腕处（刀棍的中段偏后）迎截，用力向斜上方一震，棍子可被震飞，刀子也可被震断！<br />
&nbsp; &nbsp;&nbsp; &nbsp;左臂的迎，与右脚斜上步，身体左转，右手迎击，都同时完成。<br />
&nbsp; &nbsp;&nbsp; &nbsp;右手可以用拳，也可以用武器，甚至用三尖钉、铁拳头之类暗器。如用武器或暗器，大多能一击就解决战斗。<br />
&nbsp; &nbsp;&nbsp; &nbsp;其他的，如对匕首、菜刀之类，均可如法炮制。<br />
&nbsp; &nbsp;&nbsp; &nbsp;神臂不是进攻性武器，却是防护的最佳武器，千万不可小觑！它等于在你危急的时候，又加了一道保护自己的安全屏障！<br />
