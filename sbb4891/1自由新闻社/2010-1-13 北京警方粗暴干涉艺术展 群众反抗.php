七&nbsp;&nbsp;半块砖<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;半块砖？没有开玩笑吧！半块破砖也是武器？太可笑了！对！就是半块砖！第七种武器。请看完后再笑吧，如果你还笑得出来！<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;半块砖头打天下，纯粹是街头最真实的打斗技术！<br />
&nbsp; &nbsp;&nbsp; &nbsp;“打天下”固然是口出狂言了，不过从街头狂徒口中吐出的“天下”无非是指“一条街”、“一块地盘”。<br />
&nbsp; &nbsp;&nbsp; &nbsp; 经过长年累月的实践，其打斗技巧以日臻完美，效果更显著，打法更朴素！<br />
&nbsp; &nbsp;&nbsp; &nbsp; 本篇资料即供爱好实战的朋友练习，也供不原在“半块砖”下遭头破血流之灾的朋友们研究防范之用。<br />
&nbsp; &nbsp;&nbsp; &nbsp; 一&nbsp; &nbsp; 使一块砖头变成半块砖头<br />
&nbsp; &nbsp;&nbsp; &nbsp;一块砖头，一般种重达5斤。对于激烈的打斗而言，这个重量可增加杀伤力，但它使施用者运动迟缓，而失去进攻良机。<br />
&nbsp; &nbsp;&nbsp; &nbsp;拿起一块整砖，猛力在地上或墙上一砸，砖头立即变成两个“半块砖”左右手各拿半块砖头，即可投入战斗！<br />
&nbsp; &nbsp;&nbsp; &nbsp;两手进攻的速度，几乎达到空手的速度，而攻击的威力，则增强数倍！<br />
&nbsp; &nbsp;&nbsp; &nbsp;这就是半块砖头的妙处！<br />
&nbsp; &nbsp;&nbsp; &nbsp;二&nbsp; &nbsp; 半块砖头的杀伤点<br />
&nbsp; &nbsp;&nbsp; &nbsp;一般人以为用砖头攻击人的时候，是用砖面拍的，其实不然。一个用砖老手使用砖头时，用的是两个角：即砖头上角和砖头下角。<br />
&nbsp; &nbsp;&nbsp; &nbsp;三&nbsp; &nbsp; 半块砖头的基本攻击技术<br />
&nbsp; &nbsp;&nbsp; &nbsp;1&nbsp;&nbsp;盖头顶：用砖头下角从上向下猛击对手头顶正心。<br />
&nbsp; &nbsp;&nbsp; &nbsp;2&nbsp;&nbsp;盖额头：用砖头下角从上向下猛击对手额头正心（如果打的准，盖在两眉之间效果最佳）。<br />
&nbsp; &nbsp;&nbsp; &nbsp;3&nbsp;&nbsp;断鼻梁：对方仰脸时，将砖头举平，用砖外侧的棱角盖击对方鼻梁，可将其鼻梁打断。<br />
&nbsp; &nbsp;&nbsp; &nbsp;4&nbsp;&nbsp;砸太阳穴：先将右手砖头举至耳边，然后转身甩臂发力，以砖头下角为力点，次右上方向左下方斜线攻击对方太阳穴。<br />
&nbsp; &nbsp;&nbsp; &nbsp;5&nbsp;&nbsp;砸断锁骨：右手握砖举至右耳前，然后趁对方上身后仰时，砖头从上向下猛击对方锁骨，将其锁骨砸断。<br />
&nbsp; &nbsp;&nbsp; &nbsp;6&nbsp;&nbsp;砸胸骨：右手握砖头举至耳边，左转身，甩臂发力以砖头下角为力点，从上向下砸击对方胸部骨骼。<br />
&nbsp; &nbsp;&nbsp; &nbsp;7&nbsp;&nbsp;捅胃：右手握砖，举至右肩前，然后略俯身，用打勾拳的技术向前猛击对方胃部。<br />
&nbsp; &nbsp;&nbsp; &nbsp;训练时，家在农村的朋友，可以面对树干，持砖练习，城市的朋友，可制作一个小型沙袋，面对墙壁进行练习。<br />
&nbsp; &nbsp;&nbsp; &nbsp;三&nbsp; &nbsp; 半块砖头的基本防守技术<br />
&nbsp; &nbsp;&nbsp; &nbsp;1&nbsp;&nbsp;顶拳：当对方右拳向我胸部直线猛击时，我将左手砖头竖起，以外棱正面顶击对方的拳面，使其右拳严重受伤，不能再战。<br />
&nbsp; &nbsp;&nbsp; &nbsp;2&nbsp;&nbsp;撩肘：当对手向我上盘部位直线攻击时，我一边后让位，一边用砖头外棱由下向上撩击对方肘部，使其肘部严重受伤。<br />
&nbsp; &nbsp;&nbsp; &nbsp;3&nbsp;&nbsp;断腕：对方用右摆拳攻击时，我左脚后退一步，与对方拉开距离，同时右手握砖，从右向左打平勾拳，用砖头外棱攻击对方右手腕骨，使其骨骼断裂，不能再战！<br />
&nbsp; &nbsp;&nbsp; &nbsp;4&nbsp;&nbsp;截脚：对方用脚攻击我时，我后退让位，同时用前手砖头从上向下猛击对方踢来之脚，一般攻击倮骨，胫骨，脚背甚至是膝关节位置。<br />
&nbsp; &nbsp;&nbsp; &nbsp;练习时，请同伴一名，用各种技术攻击，我持小型沙袋练习防守技术，注意不要用力过猛，以防同伴受伤！<br />
&nbsp; &nbsp;&nbsp; &nbsp;砖石随处可见，一旦遭遇实战，千万别放过此等作战武器。半块砖头的打法，堪称化腐朽为神奇，不可不知啊。</div>

							
							
							
							
