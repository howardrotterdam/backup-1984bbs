<img src="http://blogcache.artron.net/201001/13/446_1263383955d6a2.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　昨日，金盏乡008艺术区，一名艺术家（右）与一名前来抢画并撕毁海报的不明身份者厮打。当日，创意正阳艺术区、008艺术区、将府艺术区等20处将被拆迁的艺术区抱团“暖冬”，艺术家用行为、装置艺术进行维权。活动中途，一些不明身份的人冲过来撕海报、作品，引发冲突，导致现场一度混乱。本报记者 李强 摄 <br />
<br />
　　凄厉的北风刮着，20处将被拆迁的艺术区抱团“暖冬”。昨日（1月12日），待拆迁的20处艺术区共同发起的“暖冬”艺术计划在008艺术区照常举行。当代艺术教父级人物栗宪庭也亲自参加行为艺术，支持艺术家的维权活动。<br />
<br />
　　栗宪庭领衔二十余名艺术家组成人墙<br />
<br />
　　为了维护自己的权利，创意正阳艺术区、008艺术区、将府艺术区等20个可能被拆迁艺术区的艺术家抱团在一起维权。昨日，这一维权活动“暖冬20艺术区巡回创作交流计划”在008艺术区拉开第二幕。20个艺术区的艺术家纷纷用行为、装置艺术来发出心中的呐喊。<br />
<br />
　　然而，中途的小插曲却也昭示出艺术家们目前的艰难处境。就在活动进行途中，一些不知身份的人冲过来撕海报、作品，给此次的展览造成了紧张气氛。不过，这依然没有中止艺术家用艺术来维权的行为。<br />
<br />
　　当代艺术教父级人物栗宪庭也加入了人墙的行为艺术。老栗身披着棉被，与008艺术区的众多艺术家一起，站在已被拆掉的艺术区围墙处，用自己的身体组成一堵墙。此行为艺术的发起者刘玮告诉记者，此次暖冬计划的主题是“解决”，就是希望解决生存问题，“现在我们的家园被拆了，我想用这样的行为表达出我们保护家园、保护房屋的意愿。”栗宪庭表示，艺术家们维权也相当于自己的事，应该来进行帮助。<br />
<br />
　　艺术家状告房东，今日二审<br />
<br />
　　面对突如其来的拆迁厄运，创意正阳艺术区等艺术家纷纷采取行动。除了用艺术的方式来呐喊维权外，他们也一纸诉状将房东告上法庭。记者了解到，创意正阳艺术区的艺术家起诉翰石艺洲公司案已于日前在西城区人民法院首次开庭，并将于今日进行二审。<br />
<br />
　　在一审过程中，艺术家们要求翰石艺洲公司赔偿违约金、房屋押金、装修费用、搬家费等。而出庭的翰石艺洲公司一位财务人员表示，公司目前没有实际业务，自己的工资也被拖欠了几个月。由于当庭并未宣判，今天法院将就艺术家与翰石艺洲公司的这一纠纷进行二审。（记者 李健亚）<br />
