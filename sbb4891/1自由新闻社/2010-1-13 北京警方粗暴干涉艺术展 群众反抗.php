四&nbsp;&nbsp;三尖钉<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;三尖钉属于贴身暗器，暗器之“暗”，意为隐蔽、突然，令人防不胜防。三尖钉旨在增加“利器”效果，突出的是一个“毒”字！小巧轻便，易于隐藏携带，绝佳的防身利器！<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;三尖钉的制作方法有多种，大同小异，下面介绍两种制作方法：<br />
&nbsp; &nbsp;&nbsp; &nbsp;1 取两根10厘米长的铁钉，将其中一根铁钉的钉尾接在另一根铁钉的中间，两钉成直角，用布缠住，握在手中，即成三尖钉。<br />
&nbsp; &nbsp;&nbsp; &nbsp;2 用粗细适当的短木棍一根，长约15厘米，两头削尖成尖角，在木棒中间，钻一个洞，从洞中传出铁钉一枚，用铁皮包好，外用麻绳缠紧即可。<br />
&nbsp; &nbsp;&nbsp;&nbsp;将钉尖在石头上磨一磨，使其更加锋利，更有狠毒者，在顶尖上n一些毒药，让人受刺后更受一层痛苦！<br />
&nbsp; &nbsp;&nbsp;&nbsp;三尖钉的三个尖，功用各不相同，中间的尖最长，是冲锋陷阵杀敌取命先锋！&nbsp;&nbsp;<br />
&nbsp; &nbsp;&nbsp;&nbsp;如敌出拳攻我时，我可用中间的尖迎上去，钉尖定可刺穿敌人的拳头！用于进攻，穿皮透肉，轻轻爽爽毫不费力。请注意：慎勿刺击心脏部位，以免引起死亡 ！<br />
&nbsp; &nbsp; 虎口一侧的尖，适于摆拳攻击。<br />
&nbsp; &nbsp; 小指一侧的尖，即可迎击歹徒攻过来的拳脚，也可主动砸刺歹徒的身体！一器在手，威力倍增！此话丝毫不虚！<br />
&nbsp; &nbsp; 暗器的用法，在于隐蔽、突然，攻击迅猛凌厉，阴险狠毒。端的是神鬼难防！运用得当，足以让人心惊肉跳，心胆俱寒！<br />
