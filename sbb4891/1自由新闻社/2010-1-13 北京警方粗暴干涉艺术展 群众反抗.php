													<h2>野山武道精品选萃 -- 七种武器</h2>
						
						
																											<div id="postmessage_429699" class="t_msgfont">以下介绍的，是邵发明老师早期的作品，旨在让大家重温一下凶险的江湖拼杀。现在看来。这些东西可以作为实战打斗的思想去研究，做为一个武者，必须去了解和认识这种生死拼杀的格斗术。希望你能有选择的去掌握它，却不希望你有机会去应用它，因为这类技术本身就是一把双刃剑，易伤敌，更易伤及自身。阅者不可不知！ <br />
一&nbsp;&nbsp;短刀<br />
&nbsp; &nbsp;&nbsp;&nbsp;短刀易于隐藏，便于携带，是自卫防身的首选利器，短刀凶险，极易伤及人命！六路斩筋刀，专对人体四肢筋腱肌肉下手，制而不杀，是一种留生刀法。<br />
&nbsp; &nbsp; 刀的形制：斩筋刀身长为20厘米，刀柄约10厘米，离刀尖6厘米处，是刀体最宽位置，约6厘米，刀背厚2--3厘米。刀身有两个“斩位”，一为“推位”，即刀锋近刀柄处，一为“斩位”，即刀锋最宽处。<br />
&nbsp; &nbsp;&nbsp;&nbsp;六路斩筋刀：斩碗斩臂斩肩斩腿斩膝斩脚<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;斩碗二式<br />
&nbsp; &nbsp;&nbsp;&nbsp;1&nbsp; &nbsp; 这是主动攻击的例子：<br />
&nbsp; &nbsp;&nbsp; &nbsp;敌我对峙，我右手持刀，将刀掩在臀后，让敌人看不见。<br />
&nbsp; &nbsp;&nbsp; &nbsp;左脚原地微提空，右脚迅速蹬地跳起，以脚尖快速踢击敌裆部。<br />
&nbsp; &nbsp;&nbsp; &nbsp;我右脚踢击尚未到位，右手持刀忽向前攻击，以最快的速度最凌厉的功势，斩击敌手腕，将其手腕斩残！<br />
&nbsp; &nbsp;&nbsp;&nbsp;2&nbsp; &nbsp; 这是斩筋刀通用的模式：<br />
&nbsp; &nbsp;&nbsp; &nbsp;敌我对峙，双方都持刀。<br />
&nbsp; &nbsp;&nbsp; &nbsp;敌主动攻击，近身，左拳击我脸部，我急举刀用刀背向右上方挑其左臂，刀口正对敌方鼻子。<br />
&nbsp; &nbsp;&nbsp; &nbsp;敌急举右手刀砍我左颈（这是杀人刀法，左颈动脉一旦砍中，必死无疑！是最习惯的不用练就会的“杀人刀法”！）。此时，我上体以右脚为轴左旋，左脚贴地向后滑弧约90度，让开敌人攻击，同时，我右手举刀用推位迎击敌手腕，敌举刀挥砍时，其碗自动“送”在我的刀口上，自己斩断自己手腕！<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;斩臂<br />
&nbsp; &nbsp;&nbsp; &nbsp;敌我对峙，双方持刀。敌近身用左拳击我眼部，我急举刀用刀背向右上方挑开其手臂，刀口正对敌方鼻子。<br />
&nbsp; &nbsp;&nbsp;&nbsp;（1）敌举刀欲劈我时，我速以斩筋刀“推位”猛力推斩敌大臂或肘窝，将其肘窝肌筋斩断！<br />
&nbsp; &nbsp;&nbsp;&nbsp;（2）敌用刀挺刺我腹，我闪步让身，同时用刀推斩敌肘窝，将其肘窝处肌筋斩断！<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;斩肩<br />
&nbsp; &nbsp;&nbsp;&nbsp;敌我对峙，双方持刀。敌主动攻击，左拳虚晃，右手举刀向我砍来。<br />
&nbsp; &nbsp;&nbsp;&nbsp;我即右闪身，同时举刀推斩敌右手腕，<br />
&nbsp; &nbsp;&nbsp;&nbsp;随即，我右手反刀，斩向敌右肩正中，将其肩筋斩断，使其终生残疾。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;斩腿<br />
&nbsp; &nbsp;&nbsp;&nbsp;敌我对峙，双方持刀。敌主动攻击，用左拳击我眼部，我急举刀用刀背向右肩上方挑开敌左腕。<br />
&nbsp; &nbsp; 我右脚向敌左脚后面前进，同时伏身，用刀锋砍击敌左大腿肌肉（约左膝上方五寸，偏内侧）将其腿斩断。此式防上斩下，极易得手！<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;斩膝<br />
&nbsp; &nbsp; 敌我对峙，双方持刀。敌进身，左手虚晃后，举右手持刀向我砍来。<br />
&nbsp; &nbsp; 我左脚微后退，同时右手举刀以推位推斩敌右手腕，伤其右臂，使其刀失去攻杀力。<br />
&nbsp; &nbsp; 我右脚向敌中盘挺进，伏身，以刀锋从左至右横砍敌左膝窝内侧这条筋，将其斩断，使其失去缩腿之效，不能行动。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;斩脚<br />
&nbsp; &nbsp; 敌我对峙，双方持刀。敌主动攻击，近身，以右拳击我眼部，我急用刀以刀背向右肩上方挑开敌左腕。<br />
&nbsp; &nbsp; 敌用左脚踢我裆部，我下沉身，以刀锋下斩敌左脚脚背最高处。<br />
&nbsp; &nbsp; 此时，敌可能继续用右手刀斩我左颈，我可再向下伏身避其攻击，或以刀迎斩敌右手腕。脚背最高处为“冲阳穴”，出血难救，很难止血！<br />
<br />
<br />
<br />
二&nbsp;&nbsp;短棍<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp; &nbsp;断棍材料易寻，携带方便，技法刚猛霸道，深受实战实战爱好者的喜爱！杀手棍法，是一种江湖混战特技，特技是指在特殊场合运用的特殊技法，用于混战，疯狂猛烈，无人能挡！此技法及其凶狠，不是不能用，而是不可轻用！<br />
&nbsp; &nbsp;&nbsp;&nbsp;混战特技-------杀手棍<br />
&nbsp; &nbsp;&nbsp;&nbsp;杀手棍的特点是双手持棍，必须近身拚打，发挥的是“一寸断，一寸险”的优势！整体冲撞，威力极强，真正的杀手技法！！<br />
&nbsp; &nbsp;&nbsp;&nbsp;短棍规格：长度，一臂之长&nbsp; &nbsp;粗细，一握，应手即可。材料，木质铁质均可。<br />
&nbsp; &nbsp;&nbsp;&nbsp;短棍分为三部分，棍头、棍身和棍尾。持棍法：右手握棍头段，距棍头一握，左手握棍尾端，离棍尾一握。一握，即一拳距离，相当于一掌宽。<br />
&nbsp; &nbsp;&nbsp;&nbsp;杀手棍法-------捣、杵、撞<br />
&nbsp; &nbsp; 捣：即以棍头为力点向前冲击，相当于长棍中的“刺‘。练法如下：<br />
&nbsp; &nbsp; （一）轨迹练习<br />
&nbsp; &nbsp; 纵步站立，右脚在前，两脚间距与肩同宽。双手持棍，如上法。然后，双手持棍做前立云的练习，即从下向后向上向前的环运动。动作缓慢，要连贯、流畅，仔细体会：<br />
&nbsp; &nbsp; 后脚前蹬使重心缓缓前置，推动身体前移，前脚后推使重心缓缓后置，较的前蹬后推，带动身体前摇后摆。<br />
&nbsp; &nbsp; 身体的前摇后摆，其实是一个脊椎的前摇回环――用身体把棍送出去，又用身体把棍拉回来。<br />
&nbsp; &nbsp;&nbsp; &nbsp;（2）&nbsp;&nbsp;发力练习<br />
&nbsp; &nbsp; 手（棍）的前立云变成直线出，弧线回的＂扁圆＂，加快练习，力争在半秒内完成。<br />
&nbsp; &nbsp;&nbsp; &nbsp;（3）&nbsp;&nbsp;寸步捣击练习<br />
&nbsp; &nbsp; 与上面相同，不同的是多一个寸步，在寸步的基础上进行捣击练习，力点在棍头。<br />
&nbsp; &nbsp;&nbsp;&nbsp;杵&nbsp;&nbsp;即以棍尾为力点，从上向下杵击。练习如下：<br />
&nbsp; &nbsp;&nbsp;&nbsp;（一）&nbsp;&nbsp;轨迹练习<br />
&nbsp; &nbsp;&nbsp;&nbsp;纵步站立，右脚在前，两脚间距与肩同宽。双手握棍，放于右胸前。然后，双手持棍，做前立云的练习（以棍尾为标准）即右胸前向上向前向下向后的回环运动。身、手、步的协调运动基本同“捣”。<br />
&nbsp; &nbsp;&nbsp;&nbsp;（二）&nbsp; &nbsp;发力练习<br />
&nbsp; &nbsp;&nbsp;&nbsp;手（棍尾）的前立云变成斜下直线出，斜上弧线回的扁圆。加快速度，力争半秒内完成。<br />
&nbsp; &nbsp;&nbsp; &nbsp;（三）&nbsp;&nbsp;连击练习<br />
&nbsp; &nbsp;&nbsp;&nbsp;回环杵，以棍尾连续杵击三至五次为一组，要快速连贯，全力以赴！一般在原地练习，也可闪步配合寸步练习。<br />
&nbsp; &nbsp;&nbsp; &nbsp;撞&nbsp; &nbsp;以棍身中段为力点，从腹前向前向上冲击。练习如下：<br />
&nbsp; &nbsp;&nbsp;&nbsp;（一）&nbsp; &nbsp;轨迹练习<br />
&nbsp; &nbsp;&nbsp;&nbsp;纵步站立，右脚在前，两脚间距与肩同宽，双手握棍，放于腹前。<br />
&nbsp; &nbsp; 注意后脚推动身体前摇，前脚又把身体推回；身体带动棍身前送，又把棍身拉回，全身协调运动。短棍可呈水平，也可垂直，还可斜置，但都以棍身中段为力点。<br />
&nbsp; &nbsp;&nbsp;&nbsp;（二）&nbsp; &nbsp;发力练习<br />
&nbsp; &nbsp;&nbsp; &nbsp;同上，两点有异：1 棍身的前立云变成直线出弧线回“扁圆”运动。2 加快练习，不得超过半秒！<br />
&nbsp; &nbsp;&nbsp;&nbsp;（三）&nbsp; &nbsp;寸步练习<br />
&nbsp; &nbsp;&nbsp;&nbsp;在原位撞击的基础上，加上寸步练习，使撞击更猛烈！<br />
&nbsp; &nbsp;&nbsp;&nbsp;以上练习，初学者很难领会，邵先生用独特的方法让你快速感受原始的整体发力！简介如下：<br />
&nbsp; &nbsp;&nbsp;&nbsp;50斤重的石头一块，双手捧起石头，放在腹前。然后，用力把石头扔出去！当然仍的越远越好。多仍几次，细心体会。<br />
&nbsp; &nbsp; 配合上面讲解仔细体会，不要小看它，告诉你，仍石头的过程，其实就是整体发力的过程！<br />
<br />
<br />
<br />
三&nbsp;&nbsp;两头枪<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;两头枪是真正的江湖拼杀之术！之所以用“拼杀”这个词，是因为它最能代表江湖打斗的险恶！此法可用于一切以寡敌众、对付歹徒铁棍、马刀围攻的任何场面！向你展示最为凶悍江湖拼杀，电光石火间，生死立判！<br />
&nbsp; &nbsp;&nbsp; &nbsp;<br />
&nbsp; &nbsp;&nbsp; &nbsp;两头枪<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;如果被一群手持马刀、铁棍的歹徒围住，你觉得还会有生还的希望吗？有，如果你有两头枪！实施此法，唯一之要，在于“勇”！舍命的拼杀，为自己拼出一条**！<br />
&nbsp; &nbsp;&nbsp; &nbsp;两头枪制作方法：<br />
&nbsp; &nbsp;&nbsp; &nbsp;1&nbsp;&nbsp;120厘米――150厘米木棍一根，将两头削尖，分别套上一支标枪铁尖即可！<br />
&nbsp; &nbsp;&nbsp; &nbsp;2&nbsp;&nbsp;120厘米――150厘米竹子一根，两头削尖，形如矛状即可！<br />
&nbsp; &nbsp;&nbsp; &nbsp;将两头枪中间一短，间距约50厘米，各钻一个小孔，插入两根10厘米长的钢筋，插结实，外用铁皮包好，固定，中间这一段，专用于对付铁棍马刀，不怕随棍削手！<br />
&nbsp; &nbsp;&nbsp; &nbsp;（一）&nbsp;&nbsp;理论知识<br />
&nbsp; &nbsp;&nbsp; &nbsp; 原地刺杀训练，其实是发力训练，当然并非站在原地一动不动，而应随遇小步滑动，没有固定的步法可言。<br />
&nbsp; &nbsp;&nbsp; &nbsp; （二）&nbsp;&nbsp;技术指导<br />
&nbsp; &nbsp;&nbsp; &nbsp; 1 前刺：右警戒式站立，两手持枪，按前发力要点，左脚蹬地，上身前倾斜，带动两手持枪高速刺出，同时右脚顿地回颤，并借助全身回顿之力，将枪迅速拉回。<br />
&nbsp; &nbsp;&nbsp; &nbsp; 2 后刺： 同上，不过方向相反，用两头枪尾枪向身后猛刺！<br />
&nbsp; &nbsp;&nbsp; &nbsp; 3 撞： 其实是杀手棍法的一种，用枪身中段撞击，发力方法与前发力相同。<br />
&nbsp; &nbsp;&nbsp; &nbsp; 在右警戒式的基础上，蹬脚摇身，带动两手向前高速运动，以枪身中段为力点，向前猛力冲撞后，自然迅速收回！<br />
&nbsp; &nbsp;&nbsp; &nbsp; 4 扎： 用枪尾从上向下（正或斜都可）刺扎，与杀手棍中的“杵”完全相同。<br />
&nbsp; &nbsp;&nbsp; &nbsp; 在右警戒式站立，将枪向右上方提起，全身猛向下一沉，带动两手从上向下高速运动，枪尾猛向下一扎，迅速收回！<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;（三）&nbsp; &nbsp;跑步刺杀训练<br />
&nbsp; &nbsp;&nbsp; &nbsp; 5米外选一目标，你两手持枪，向前猛跑，高速冲近后，用各种枪法向目标猛击。 背朝目标，快速后退，用枪尾向后猛扎!<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;(四）&nbsp; &nbsp;挡刺训练<br />
&nbsp; &nbsp;&nbsp; &nbsp; 1 当敌持刀向我左肩、左颈砍来时，我用枪身中段向左上方一挡，随即离敌最近的枪头向敌猛刺，一刺即回！<br />
&nbsp; &nbsp;&nbsp; &nbsp; 2 敌向我左中，下盘攻来，我用枪向左下一挡，复一枪刺去！<br />
&nbsp; &nbsp;&nbsp; &nbsp; 4无论敌人如何攻击，我都一挡一刺，便挡便刺，可在一秒内洞穿对手胸、腹！<br />
&nbsp; &nbsp;&nbsp; &nbsp; 一边回旋，一边刺，是技法关键，挡刺之间没有停顿，边旋边压，在抵制敌人攻势的同时，为自己的刺作好预动，这样，更流畅，更自然，更狠，更快！<br />
　　劲力来源于杀手棍，技术来源是刀法中的＂缠头裹脑“，明眼人一看便知！！<br />
&nbsp; &nbsp;&nbsp; &nbsp;杀手棍、两头枪是邵发明先生多年研究所得，曾耗心血无数！轻易便可洞穿胸、颈、脑等部位。此技只可用于性命攸关时保命之用，不到万不得已，绝对禁用！请考虑后果！阅者慎勿外传，免入匪手！<br />
