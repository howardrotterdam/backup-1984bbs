五&nbsp;&nbsp;三变锤<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;现在的武器，都在向小型化、小巧化发展，但是杀伤力也随之减弱。邵先生经过潜心研究，终于推出了易于隐藏、便于携带的武器：“三变锤”！此种武器，利用器械本身的威力代替功力，稍加训练便可应付群敌！狂轰乱炸，当者立仆！<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;三变锤为何物？普通铁挂锁是也！这儿介绍的是它的训练和使用的方法。<br />
&nbsp; &nbsp;&nbsp; &nbsp;锤的携带：选铁挂锁一把，重约一斤。将锁弯扣打开，挂于腰间皮带上，即可。临用时，将手握住锁体抽出并在腰上一扣，锁即挂锁，即可应用！<br />
&nbsp; &nbsp;&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp; &nbsp;第一变：&nbsp; &nbsp;杀手重锤<br />
&nbsp; &nbsp;&nbsp; &nbsp;在随手暗器中，没有什么比“杀手重锤”更具杀伤力！<br />
&nbsp; &nbsp;&nbsp; &nbsp;1 手握重锤作直拳击打，威力倍增！由于手部重量增加，杀伤威力也激增数倍！兹举数例：<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;（1） 敌我对峙时，我用左手虚晃敌面，令其迷惑，右手握锤的重拳猛击对方的鼻子，足以将其鼻骨打断，昏死过去！<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;（2）敌我对峙，我握锤右手忽打出劲爆直拳，直击敌面，足以将其打晕，当场昏倒！<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;（3）敌我对峙，我握锤右手忽打出劲爆撞锤，猛击敌心窝，敌必被打的当场吐血，昏死过去！<br />
&nbsp; &nbsp;&nbsp; &nbsp;2 手握重锤作砸打动作，则锤到骨折，锤到脑裂！“重锤”最适合从上向下击打，其杀伤力是普通击打的5----10倍！兹举数例：<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; （1）当敌用左指拳攻我上盘或中盘时，我右脚右闪，身右闪，让开敌人攻击的同时，右手握锤猛砸敌左肘关节，凡击中，关节必断！其臂立废！<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;（2）敌用脚踢我裆部或踹我腹部时，我后闪身的同时，右手握锤以锁的圆弯猛砸敌脚背，胫骨，外倮骨，其骨必断！<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;（3）我左手虚晃后，右手握重锤，猛砸敌额骨、鼻子或锁骨、胸骨，可制敌伤、残、死！<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;（4）我以左脚猛踢敌裆部，待敌下防时，抓敌头发猛然下拉，右手握锤猛砸敌后颈，则敌立亡！<br />
&nbsp; &nbsp;&nbsp; &nbsp;3&nbsp;&nbsp;手握重锤横向挥打，也可取得重击效果！兹举二例：<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;（1）敌用左拳击我胸部，我右手握锤下劈敌手臂，速以右手拳背反击敌脸部！<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;（2）我左手虚晃后，右手握锤以摆拳模式攻击，横击敌左侧太阳、听会穴、耳后穴，均可杀人于瞬间！<br />
&nbsp; &nbsp;&nbsp; &nbsp;第二变&nbsp; &nbsp; 流星大锤<br />
&nbsp; &nbsp;&nbsp; &nbsp;流星大锤专用于对付群敌，其杀伤力犹如“重磅炸弹”，人莫能当！<br />
&nbsp; &nbsp;&nbsp; &nbsp;若有准备，可备布袋一只，将挂锁放入袋内即可。平时临用可将衣服脱下，裹住铁挂锁，用手抓住衣角，即成流星大锤！<br />
&nbsp; &nbsp;&nbsp; &nbsp;实战格斗时，只须将“流星大锤”甩起，成“正五花”“反五花”或“平五花”，两脚灵活走动，以甩击敌人的头部为目标，中者无不血光迸现，立扑，无力再战！这是群战的极佳武器！<br />
&nbsp; &nbsp;&nbsp; &nbsp;第三变&nbsp; &nbsp; 甩手飞锤<br />
&nbsp; &nbsp;&nbsp; &nbsp;关键时刻，将“流星大锤”飞甩而出，便是“甩手飞锤”，由于其重量的缘故，杀伤力极为惊人！邵先生作过试验，对它的杀伤效果非常满意！当然，这只是在关键时刻，因为甩手飞锤只能使用一次！<br />
