【本报讯】连日来不断逃避，拒向公众交代会否在明天补选日投票的特首曾荫权，昨晚 10时 30分忽然透过政府新闻处发出声明，以今次立法会补选并非正常补选为由，正式宣布他与属下问责团队官员，不会于 5.16投票日投票，又扬言会修订法例阻止再有五区公投出现。泛民立法会议员对曾如此鬼祟交代不投票决定感到愤怒，指摘特首带头不投票，根本就是向反公投的北京下跪叩头、势令香港沦为国际笑话，也显示曾荫权根本无意落实真普选，只会刺激更多市民于明天出来投票。<br />
记者：莫剑弦、许伟贤、雷子乐<br />
<br />
曾荫权上周在立法会答问大会上表示，会就是否投票向公众交代，连日来他出席公开活动，不断有传媒向他查询，他一律拒绝响应。他昨晨出席新界社团联会活动时，依旧拒绝回答 5.16会否投票，只是摊大手掌向传媒微笑挥手，然后登车离去，也是他本周内，第 4度在公开场合拒绝就投票问题表态。<br />
<br />
鬼祟发声明　避传媒追问<br />
<br />
<img src="http://thumbsnap.com/i/prmwhM5z.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
曾荫权宣布他与其问责团队明天不会投票。数据图片<br />
<br />
不过，曾荫权于昨晚 10时 30分，忽然透过政府新闻处发表声明，指由于立法会议席出缺，所以政府依法安排五区进行补选，但社会上的主流意见都认为，今次补选本无需要、甚至有市民认为是滥用选举程序，浪费公帑的行径，而政府早前已表明会考虑修改法例，以避免同样事情再发生，所以他小心考虑后，决定在这次补选中不会投票，而他的问责团队也认同有关观点，也决定不会投票。<br />
<br />
曾荫权扬言，今次是他个人与问责团队的个人决定，公务员不应、也不会受有关决定影响；公务员投票与否，是他们的个人决定，政府会完全尊重。<br />
<br />
曾荫权今次作出如此重大决定，违反过去惯例，由他及其问责班子带头宣布不在补选中投票，令政界哗然。民主党创党主席李柱铭指投票属公民权利，今次曾荫权及其班子因北京反对而不投票，令人质疑他们屈服于北京政治压力而放弃投票权。职工盟立法会议员李卓人抨击曾荫权因为北京反对公投，不惜向北京下跪叩头，自愿带头不投票来打击公投，出卖及损害香港核心价值，令人愤怒。<br />
