<br />
公民党秘书长陈家洛则说，政务司司长唐英年声称政府建议获得「过半数市民支持」，但报告的附录透露，在咨询期收到约 47,200份意见书之中，约 42,700份内容相同，即只有 4,500份不是「倒模」之作；同时，在收到的逾 160万个签名之中，有约 160万个为工联会发起的「政制向前走大联盟」所收集，即只有约 8,000个为其它人士、政党或团体所收集。如此「支持」，何止自欺欺人？简直是睁着眼睛说瞎话。五区公投运动是人民说真话的关键时刻。</div>

							
							
							
							
