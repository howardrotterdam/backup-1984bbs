<br />
想想你的亲戚、朋友、同学、同事、邻居，以至街上那位正在清理垃圾的妇女──告诉你她的时薪只有二十元，每天工作十二小时。<br />
<br />
以上的问题，特区政府和立法会内的功能组别议员都不屑理会，因为他们毋须对你负责。<br />
