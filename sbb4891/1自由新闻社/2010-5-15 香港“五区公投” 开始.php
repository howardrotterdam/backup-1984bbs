													<h2>苹论：「忠于自己，就算输了，也是赢了」</h2>
						
						
																											<div id="postmessage_617142" class="t_msgfont">Apple Daily 苹果日报网上版 －－2010-05-15<br />
苹论：「忠于自己，就算输了，也是赢了」<br />
<br />
「忠于自己，就算选举输了，也是赢了；不忠于自己，就算选举赢了，也是输了。」<br />
<br />
这是担任奥巴马竞选总裁的迈尔丝（ Betsy Myers）上周去台湾演讲时，引述奥巴马竞选的核心信念。明天是五区补选公投的日子。笔者愿以这段话赠送给所有的参选者、曾特首众高官及所有杯葛这次投票的建制派，也赠送给要去投票及不去投票的合资格选民。<br />
<br />
选举讲输赢，公投提出的议题讲成败。若以发起辞职补选的五子能否当选来论输赢，那么按民调他们是赢面很大的；但若论公投成功还是失败，那么多数议论都认为是：公投必败！<br />
<br />
从公投的结果会不会被中央和特府接受的政治现实来看，不用说，它是肯定失败的。从投票率来看，正式公投的有效投票率多设定要占选民的五成，而对议题肯定或否定的取舍率也要占投票者的五成。这次补选的选前民调显示，投票率最多三成七，更有民调显示投票率可能低见 12%。因此，对这次选举所取得的公投效应，不容乐观。社民连前天提出争取的投票率是三成，并说若达三成，即代表公投胜利。<br />
<br />
就公投意义上说，三成投票率当然不能说是胜利。但这次并非宪制下的法理公投，而是变相公投，它是在中央、特府和建制派打压、杯葛而泛民最大党也不参与的情势下，由五位议员钻法制的空子而搞出来的。因为普选议题经 20年争取扰攘而在中央压制下无法实现，因为 2012政改方案是一个向民主倒退的方案，由此透露中央承诺的 17、 20年双普选有太多假普选因素，因为香港无法举行一次真正的法理公投，让市民表达对真普选的意愿，这就促使泛民五子制造一个让市民投票表达意见的变相公投的机会。<br />
<br />
很明显，这个难得的机会，在打压、杯葛和不参与的情况下，不大可能获得正式法理公投的得票率，但这是否意味着这次公投运动就输了呢？笔者在文前引用迈尔丝的话，正可以回答这问题。不错，忠于自己，输了也是赢了；不忠于自己，赢了也是输了。<br />
<br />
这绝非阿 Q精神，而是体现真正的人生和对人类的信念。人生是漫长的，一次输赢不能定一生输赢，人类争取建立一个更好的制度也是漫长的，同样不能以一次输赢来定终极输赢。忠于自己，即在自己的信念和价值观主导下行事，一次输了但终其一生你是赢的；不忠于自己，被现实政治利益主导而左摇右摆，比如在当年左派报章都承认是左派「锄奸队」杀林彬的铁证下，今天民建联的议员却左支右绌地否认，这样行事纵使可获一时的政治利益，但这虚伪狡诈的人生，终究是输的。<br />
<br />
泛民五子是否忠于自己？不支持公投的泛民政治人物是否忠于自己？曾特首和众高官对是否投票的闪缩，是否忠于自己？明天前往投票或不投票的市民，是否忠于自己？这不是一时的输赢，而是一生的输赢。而变相公投在香港政制发展上，也不是一次输赢，而是具有里程碑意义的、走向终极民主的输赢。<br />
<br />
忠于自己的选民，你们仍然可以不去投票，只要你衷心相信：一、 2017、 2020有真正的可以踢走功能组别的真普选；二、你认为「一国」不断干预香港这一制乃理所当然；三、香港特区在「第二个权力中心」的牵制下，各项政策都能令你很满意；又或者四、你不认同民主制度是普世价值，或对香港可以实现民主绝望。<br />
<br />
若你不是衷心相信以上四点，而你又要忠于自己，那么明天请往投票站。<br />
<br />
忠于自己，美国黑人经上百年抗争，黑人奥巴马创造历史，赢了；忠于自己，日本不满「万年执政」的自民党的选民经过数十年的输，去年八月终于赢了，实现了政党轮替。因此，香港明天的变相公投，只要选民忠于自己，那么不管输赢，终究还是赢了。<br />
