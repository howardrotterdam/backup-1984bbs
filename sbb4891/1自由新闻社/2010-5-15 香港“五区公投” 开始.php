													<h2>五区公投　明日投票　为良心　为公义　为民主出一分力　年轻移民返港撑公投</h2>
						
						
																											<div id="postmessage_617107" class="t_msgfont">Apple Daily 苹果日报网上版 －－2010-05-15<br />
五区公投　明日投票<br />
为良心　为公义　为民主出一分力　年轻移民返港撑公投<br />
<br />
 <img src="http://thumbsnap.com/i/vCgNMe2Z.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
左起： Joseph、 Mandy和 Michael三个移民海外的港人，为了香港民主，自费返港撑公投。王子俊摄<br />
<br />
投票？不投票？一些香港人可能仍然举棋不定，究竟明日是否多走几步路，到附近的票站投票。 3个已移居外地的香港人反而不愿隔岸观火，在 5.16前夕特意请假，自费买机票千里迢迢回到他们出生和成长的地方，要为香港的民主出一分力。他们只抱着一个信念，只在乎对或错，不问输与赢，凭良心做应做的事。<br />
<br />
记者：麦志荣、蔡元贵<br />
<br />
于 97年中七毕业后到美国留学的陈敏怡（ Mandy），毕业后在美国的化验所工作。留学前她在香港已登记为选民，今次为了 5.16公投，专程回来投票。<br />
<br />
Mandy：香港人惊乜S<br />
<br />
Mandy一直没有忘记母亲的教诲，叮嘱她不要忘记自己是中国人，因此当年她第一时间将 BNO（英国国民（海外）护照）换成香港特区护照。但近年四川大地震揭发内地豆腐渣工程、毒奶粉受害孩子的家长想讨回公道反被判监等事件，都令她觉得有太多的不公义，令她觉得不能让香港变成内地那样。<br />
<br />
多了她一票，未必能改变甚么，可是 Mandy觉得：「香港人太过计算成败得失，我原则系应做就去做，要民主就去争取，香港人惊乜S呢？」<br />
<br />
与 Mandy一起回港的还有高国梁（ Joseph）和关天佑（ Michael）。<br />
<br />
Joseph：功能组别不公<br />
<br />
Joseph在 89年时还是中二学生，和很多香港人一样从电视的转播中，目击了六四镇压，坦克和枪声令他开始思考和关心政治问题。翌年他随家人移民美国，现时从事计算机业。<br />
<br />
眼见美国首个黑人总统奥巴马在选举中产生，反观香港到现在还在争取最基本的一人一票普选，政治制度和政府政策仍受功能组别左右而非常不公平，他对保皇党经常振振有词批评美国选举其实也不民主，另有一番体会：「右桓鲋贫认低昝溃但打个比喻，彰拦，你唔食饭，仲可以拣食面，但障愀郏唔食饭，就要食屎！」<br />
<br />
Michael中五毕业就离开了香港，原因是他的父亲一生都在逃避共产党。他父亲文革时由内地偷渡来港，常常在亲友前破口大骂共产党，更赶在回归前举家移民加拿大。父亲的心结令他自小对中国历史和政治产生了浓厚兴趣，经过多年的探索，他发现中国的问题归根究柢都是因为没有民主。<br />
<br />
Michael：不再做旁观者<br />
<br />
现时他在美国一间手电芯片公司工作，每年在网上看到香港 7.1游行和六四烛光集会的片段，都很想成为其中一分子。今次公投是香港民主的转折点，他决定不再做旁观者。 Michael和 Joseph都没有投票权，但会呼吁在香港的亲友投票。<br />
<br />
三个异乡归客刚回港，昨晚立即参加五区公投运动造势大会，更计划 5.16做义工，在票站外拉票。问三人究竟为了甚么，他们异口同声：「为了良心，为了公义！」就算普选没有时间表、没有路线图，带领他们向前的仍有良心和公义。</div>

							
							
							
							
