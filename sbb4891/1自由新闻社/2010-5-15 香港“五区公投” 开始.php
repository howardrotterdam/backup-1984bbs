或许你尚能忍受各种不公义。不过，那些你爱的人，他们是否同样能够忍受？你忍心看见他们活在这样的环境下吗？每一个损害市民利益的议案被通过、每一个家园被掠夺、每一栋旧楼宇被铲平、每一个市民遭不公对待、每一项权利被剥夺，都会令他们的笑脸消失，剩下苦涩的愁容和哀戚的眼泪。<br />
