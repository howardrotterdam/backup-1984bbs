想想你的父母，他们为了节省一丝生活费，病了不敢求诊，累了不敢乘车，热了甚至不敢开电风扇；每天弯着腰，细[着眼，以抖震的脚步推着载满纸皮的手推车横过长路，旁边的车辆呼啸而过，他们的脆弱身躯彷佛越缩越小。<br />
<br />
想想你的伴侣，你们互相扶持，一起努力，渴望置业，建立一个家，看见现有的楼盘被标上天文数字，辛苦存钱买入后，却惊见楼盘面积被当初承诺的小。<br />
<br />
