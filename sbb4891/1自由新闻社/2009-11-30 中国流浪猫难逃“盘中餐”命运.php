这次大规模营救行动彰显了日渐高涨的中国动物权利意识和动物权利保护行动，但同时也引起了其他方面的担忧。一位姓王（音）的营救志愿者对新华社说，这些猫被注射了兴奋剂，以便让它们在运输途中存活，他预计随着药效减弱，将会有更多的猫死亡。<br />
<br />
<br />
活下来的猫的长期生活也是个难题。由于中国尚无常设的动物收容所容纳被遗弃的宠物，这些猫得到了特别的照顾，被放在天津一所小学的几间空教室里的笼子中，等着被人认领或是收养。它们的食物依靠公众捐赠。<br />
<br />
<br />
中国小动物保护协会的会长卢荻告诉新华社，天津将建立一个动物收容所，收容这些猫，这是在中国10个城市建立收容所的更大规模行动的一部分。<br />
<br />
<br />
但前文所述的王姓志愿者说，他怀疑被救助的猫只有20%多能被新收容所收容。据新华社报导，他说，如果我们放了剩下的那80%，它们很可能又被人抓去卖掉。<br />
