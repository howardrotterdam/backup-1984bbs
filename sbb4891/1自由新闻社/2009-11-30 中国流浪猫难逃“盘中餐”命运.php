Associated Press<br />
广州某市场里待售的猫咪<br />
据新华社报导，天津的爱猫人士上周救出了大约1,300只本来会落入广东肉贩手中的猫。这些猫是在天津一家商店的笼子里发现的，其中一些已经死亡，包括一只怀孕的母猫。<br />
