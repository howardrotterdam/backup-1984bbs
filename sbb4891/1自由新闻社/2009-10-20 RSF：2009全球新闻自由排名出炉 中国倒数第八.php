中国则继续名列世界最压迫新闻自由的国家，令自由之家感到失望。报告说，北京去年举办奥运，中国理当信守在奥运期间保证新闻自由的承诺，改善其纪录，没想到中国新闻自由仍然殿後。<br />
<br />
「自由之家」系美国罗斯福总统夫人艾莲诺（Eleanor Roosevelt）及一批爱好自由民主人士发起成立，为超党派之非营利组织，该组织自一九八○年起每年发表的调查报告，是目前新闻自由度最具权威的评量报告之一。<br />
<br />
<strong>中国压迫加深<br />
香港倒退为部分自由</strong><br />
<br />
香港丶意大利和以色列今年都倒退为部分自由地区，台湾虽仍列入完全自由地区，但排名跌落为亚洲第二。报告中称赞台湾坚守司法独立丶经济自由丶媒体高度竞争丶新闻工作人员很少遭受暴力丶本地及外国记者都能自由采访新闻。不过，自由之家特别提到去年十一月中国海协会长陈云林访台期间的示威活动，台湾出现两极对立，政府和非政府成员都传出对编辑内容和采访记者施压骚扰。<br />
<br />
自由之家表示，在一百九十五个国家和地区中，有七十个国家被列入自由地区，约占三成六；另有六十一个列入部分自由，占三成一；另有六十四个列入不自由地区，其中，新闻自由度最糟的国家是缅甸丶古巴丶厄利垂亚丶利比亚丶北韩和土库曼。<br />
<br />
与去年报告相比，显示全球新闻自由度小幅恶化，去年有七十二个国家和地区列为完全自由，这也是世界新闻自由连续第七年倒退。报告并点名，过去五年间，有不少新兴民主国家的新闻自由出现相当幅度的倒退，包括墨西哥丶阿根廷丶秘鲁丶泰国丶菲律宾和塞内加尔。<br />
<br />
仅少数国家的新闻自由明显改善。自由之家特别称赞南亚的马尔地夫通过新宪法，保护言论自由，并释放被监禁的记者，故从不自由等级升级为部分自由。南美的盖亚那，也因记者受攻击的情况减少，及政府决定取消对主要独立报纸的广告杯葛，从部分自由升级为自由。</div>

							
							
							
							
