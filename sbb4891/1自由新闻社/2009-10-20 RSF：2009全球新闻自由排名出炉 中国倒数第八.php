													<h2>记者无国界：中国新闻自由排名倒数第八</h2>
						
						
																											<div id="postmessage_295112" class="t_msgfont">VOA<br />
<br />
记者无国界组织发表2009年度新闻自由指数排名报告，中国排名倒数第八，仅比老挝、古巴、缅甸、伊朗、北韩等国略胜一筹。该组织称，北京当局继续控制新闻、对网络实行新闻检查、任意抓捕记者、博客作者、持不同政见者和人权活动人士等问题使得中国新闻自由的排名遥遥落后。<br />
<br />
致力于维护新闻自由和记者权益的国际组织记者无国界在新发表的报告中头一句话就是，对政治大权的把持今年再度对新闻出版自由构成极大妨碍。报告说，最不尊重新闻出版自由的亚洲国家是处在世界排名末尾的三个国家北韩、老挝和缅甸。<br />
<br />
*排名退后 中国168 *<br />
<br />
中国在这份总共175个国家的名单中排名第168，倒数第八，比中国去年的排名(第167位)退后，但是比排在其后的老挝、古巴、缅甸、伊朗、北韩等国稍微靠前。<br />
<br />
*布罗塞尔：中宣部限制新闻自由*<br />
<br />
记者无国界总部亚洲问题研究员布罗塞尔在解释中国排名退后的原因时对美国之音表示，中国媒体近年来以及在北京奥运会期间虽然有很多改变，但是在总体的趋势上，共产党的宣传部门仍然在实行种种限制新闻自由的政策。<br />
<br />
他说：“中国这个排名主要是由于中国政府的各种新闻政策，比如对互联网的监管检查，宣传部对新闻的控制，还有拘押数十名记者以及在西藏和新疆实行的新闻和信息管制。”<br />
<br />
*网上公布政见&nbsp;&nbsp;郭泉因言获罪&nbsp;&nbsp;*<br />
<br />
关于中国政治活动人士郭泉因在互联网上公开发表个人政见及组建政党的文件和言论而被中国江苏省一地方法院以颠覆国家政权罪判刑10年一案，在法国巴黎的记者无国界组织研究员布罗塞尔说：<br />
<br />
“在互联网上只是以和平方式表达个人见解和批评言论而被判处重刑，这不是第一个，恐怕也不会是最后一个案子。我认为，这也是为什么中国在新闻自由指数排名的表现如此之差的原因之一。<br />
<br />
这位记者无国界的研究人员还表示，他注意到中国领导人胡锦涛近日在北京举行的世界媒体峰会上关于保护外国记者在中国采访权利的表态。<br />
<br />
*布罗塞尔：对胡锦涛听其言更须观其行*<br />
<br />
他说：“这位国家主席发表令人振奋的讲话，很好。但是我们需要看到具体的步骤，尤其是，为什么很多国际媒体的网站在中国受到封锁，为什么像美国之音、自由亚洲、英国广播公司的广播节目受到干扰，为什么外国记者所报道的这么多议题和内容遭到删除屏蔽，为什么外国投资者在申请传播营业执照时遇到这么多困难呢？所以，我认为，要改善中国的新闻出版自由，我们需要的是行动和承担义务，而不仅仅是口头讲话。”<br />
<br />
*布罗塞尔：理应中外一视同仁*<br />
<br />
布罗塞尔指出，中国政府不仅应该保护外国记者在中国的采访权利，也应该保护中国记者和博客作者的新闻出版自由。他认为，中国媒体工作者在中国进行宪法和法律允许的新闻出版活动理应得到政府的平等对待。<br />
<br />
在记者无国界今年发表的新闻出版自由指数年度报告中，美国与卢森堡和英国并列第20位，美国由于奥巴马政府表现得对媒体较前任温和而在排行榜上上升了将近20位。排在前5名的国家依次是丹麦、芬兰、爱尔兰、挪威、瑞典。排在倒数第一的国家跟去年一样，也是非洲的厄利特里亚。</div>

							
							
							
							
