<br />
五十三岁的夏克来自伦敦北部，是三个孩子的父亲。<br />
<br />
二○○七年九月在新疆乌鲁木齐被机场海关查获携带价值廿五万英镑、重达四公斤的海洛因。夏克被判处死刑，但英方辩护律师声称夏克患有精神病，被其它毒犯利用才携带毒品，夏克坚称自己不知道携带这些毒品，但都不被法院采纳。<br />
