　 <br />
<br />
中共定今天上午十点半处决英国籍毒犯夏克，半年来英方一直努力交涉，希望中国能改变作法，甚至在最后时刻展开与中国的「疯狂谈判」。但双方交涉十次，全被中方拒绝。中国专家称，西方常以司法独立为由要中国勿插足像赖昌星案，但这次却要让中国政府干预司法独立。<br />
