中共决定今天执行死刑，在处死前夕，夏克的家人赶到中国，纷纷递出陈情书，向中国领导人、中国人大请愿，英国驻中国大使也前往新疆探监，连英国首相布朗甚至为此致函中共国务院总理温家宝，希望中国网开一面，免除夏克一死。<br />
<br />
夏克的家属昨天上午到新疆监狱探视他最后一面，夏克与家人相处约一个半小时。<br />
<br />
协助夏克的人权机构「缓期执行死刑」表示，仍抱着希望，中国可能缓期执行死刑，「中国曾在最后一分钟缓期执行死刑，我们没有理由认为不会发生。」但夏克的死刑若最终执行，他将成为五十年来在中国被处决的第一个欧洲联盟公民。<br />
