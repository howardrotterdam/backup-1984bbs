													<h2>高干子女占亿元富豪91%</h2>
						
						
																											<div id="postmessage_192442" class="t_msgfont">6月26日，《时代周报》刊发了记者韩洪刚采写的报道，报道在导语中称：“在日前召开的政协十一届常委会上，中国财富的‘集中度’正在受到政协常委和委员的热切关注。蔡继明委员说：‘中国权威部门的一份报告显示，0.4％的人掌握了70％的财富，财富集中度高于美国。’”<br />
随后，该记者在报道中写道：“据国务院研究室、中央党校研究室、中宣部研究室、中国社科院等部门一份联合调查报告的数据,截至2006年3月底，中国内地私人拥有财产(不含在境外、外国的财产)超过5000万元以上的有27310人，超过1亿元以上的有3220人。在超过1亿元以上的富豪当中，有2932人是高干子女。他们占据了亿元户的91%，拥有资产20450余亿元。”<br />
这篇报道刊发后立刻被众多网站、论坛、博客，以及传统媒体转载、转贴。数据来源被“简化”成蔡继明的发言，将这位曾因倡导假日改革成媒体焦点的全国政协委员再次推上舆论的风口浪尖。<br />
中国社科院的“当代中国社会流动”课题负责人、中国社科院研究员陆学艺说：“‘高干子女占超亿元富豪人数的91%’，是不可信的，因为无法统计，并且比例数还精确到个位数，这怎么可能？”<br />
7月31日,记者韩洪刚承认，这段数据不是引自蔡继明委员的发言，而是引自互联网上转载的某位国内经济学者2006年写的一篇文章。<br />
随后，记者又与该经济学者联系核实。该学者称此数据引自当年的互联网，他说：“当时网上（这组数据）非常流行”。<br />
国务院研究室、中央党校研究室相关负责人则告诉记者，这组数据是假的，两年前曾在网上炒作过，最早来自海外某刊物。<br />
早在2006年10月19日，境外星岛环球网就刊发了题为“官方报告：中国亿万富豪九成以上是高干子女”的报道。报道称：“官方研究机构的调查报告披露：在金融、外贸、国土开发、大型工程、证券五大领域中，担任主要职务的基本上都是高干子弟。中国的亿万富豪九成以上是高干子女，其中有2900多名高干子女共拥有资产二万多亿。国务院研究室、中央党校研究室、中国社会科学院等部门近日出炉一份关于社会经济状况的调查报告，详细记录了社会不同阶层的经济收入。”这不知是不是网上最早出现的此虚假报告的来源，但此篇报道被传载、引用的频率很高。<br />
中国人民大学新闻学院副院长、舆论研究所所长喻国明在解读此类现象时认为，社会贫富差距已成为目前影响社会稳定问题的一个重要因素。在这种情况下，这方面的虚假数据就很容易给人们造成一种逻辑上的真实。其实，这正是社会的一种预警信号。他认为，政府部门应针对这些预警做出相应的政策性的调整和安排，切实解决民众关心的热点问题；此外，还应给公众创建一种情绪宣泄的平台，使这种社会情绪得以舒解。<br />
