																											<div id="postmessage_195514" class="t_msgfont">来自冉云飞的Twitter&nbsp;&nbsp;:&nbsp; &nbsp;人民网五毛记者的文章《一组虚假数据是如何在网上网下以讹传讹的？――关于“高干子女占超亿元富豪人数91%”的新闻调查》是如何造假的，请大家锐推《时代周报》记者彭晓芸的有力反驳。<br />
<br />
<a href="http://www.bullogger.com/blogs/pxy/archives/312593.aspx" target="_blank">http://www.bullogger.com/blogs/pxy/archives/312593.aspx</a><br />
<br />
<br />
本声明在天涯我自己的博客存在了一夜，首先是被关闭评论，今天则被彻底删除了。我终于学会了翻墙，学会了用牛博国际，感谢人民，感谢人民网。<br />
<br />
关于时代周报《贫富分化急遽加大的危险》一文的非官方说明<br />
<br />
&nbsp; &nbsp;2009年08月05日15:38，人民网在其网站的头条位置挂出一篇署名为记者“唐维红、张玉珂、常红”报道文章，标题为：《一组虚假数据是如何在网上网下以讹传讹的？――关于“高干子女占超亿元富豪人数91%”的新闻调查》（以下简称“调查”）。<br />
<br />
&nbsp; &nbsp; 该篇报道指出,《时代周报》6月29日刊发于评论专叠C12舆情版的文章《贫富分化急遽加大的危险》引述的一组数据涉嫌虚假，被某些不负责任的网站转载、转帖、“简化”走样后，最后导致“在网上网下流传，以讹传讹”。报道指出，本报记者韩洪刚未经采访而引述政协委员蔡继明在某次会议上的发言，造成不良影响。<br />
<br />
&nbsp; &nbsp; 本报刊发该篇述评的由头，是今年6月19日全国政协机关报《人民政协报》2版正式发表的一篇报道，称有政协委员指出“0.4%的人占有70%的财富”。本报记者就此向该文中提到的全国政协委员蔡继明电话采访求证过。经编前会认真讨论，本报采信了全国政协机关报《人民政协报》的这一说法，并作为述评的新闻由头。由于本报述评已在6月29日见报，《人民政协报》7月1日2版相关“更正”，本报并不知情。<br />
<br />
&nbsp; &nbsp; 本报文中提及的数据“亿万富翁91%是高干子弟”出处源自新华网转载《上海证券报》2006年10月20日A20版的文章《盛世危言：一组组令人心惊的数字背后》，作者为北京科技大学管理学院知名经济学家、教授赵晓。赵晓文章刊登在权威的《上海证券报》上。<br />
<br />
 一、本报6月29日见报的述评文章标题是《贫富分化急遽加大的危险》，而非部分网络擅改的标题《高干子女占超亿元富豪人数91%》。<br />
<br />
&nbsp; &nbsp; 人民网的“调查”一文写作建立在并没有看到本报报纸的基础上（本报在全国各大城市均有售，北京各大报摊的显著位置均可见到《时代周报》的销售），这本身就是非常不严谨和不公正的，其批驳的文章是各大门户网站自行转载并且进行了篡改的文章，人民网根据网络转载将这篇文章标题命名为《高干子女占超亿元富豪人数91%》，可惜，这恰恰不是《时代周报》的文章，但人民网仍然误以为是《时代周报》原文，并且在此基础上进行了质疑。<br />
<br />
 二、人民网指《时代周报》记者未采访蔡继明委员，这与事实不符。本报记者不仅两次电话采访了蔡继明委员，而且保留着采访笔录。<br />
<br />
而 “调查”一文也指出经网络篡改标题和内容转载后改变了原义――“《时代周报》并未说明后一个数据是他（蔡继明委员）所说，但其他媒体再次引用《时代周报》数据时，就变成了：‘蔡继明委员在近日召开的全国政协十一届常委会第六次会议上，援引一份由国务院研究室、中央党校研究室等部门一份联合调查报告的数据。 ’”。<br />
<br />
&nbsp; &nbsp;这里，人民网亦清晰无误地证明了本报述评文章经过网络媒体的转载以及拼凑剪裁之后，已经完全违背了本报该文的原意。比如，本报的这篇述评文章，实际上只是提供了几组数据、几种说法相互佐证，并未明确指出究竟哪一组数据是最权威或者最可信的，作为评论版面上的文章，提出几种说法，以论证财富分化问题在中国社会可能造成的危险，只是一种或然性的探讨，并没有遽下结论，这是全球各地媒体评论性文章惯用的合法合理合情做法，没有违背新闻媒体的职业道德和评论员的专业精神。<br />
<br />
三、蔡继明提出抗议的，不是针对本报的文章，而是将引述内容出错的网络转载文章误以为是本报的文章内容。<br />
<br />
 其实，人民网的文章就已经说明了网络媒体在转载过程中，重新提炼观点的时候，误将其他数据“栽赃”给了蔡继明委员。这也是导致蔡继明委员在网上发表博客文章的根本原因。但由于蔡继明委员没有看到本报的报道原文，只是看到流传在网络上的各种篡改后的不实版本，而网络媒体又频频引用“时代周报”的名义进行重新演绎，蔡继明委员误以为就是本报的报道，这也情有可原。如果他因此而去强烈否认曾经接受过本报记者的采访，我们也觉得可以理解，因为谁也不愿意自己的言论被歪曲报道。而事实是，本报原文并没有歪曲他的言论。<br />
<br />
四、本报述评文章《贫富分化急遽加大的危险》一文的由头来源于国家权威的机关报――《人民政协报》，编发时并经过了仔细核对。<br />
<br />
&nbsp; &nbsp;舆情版责任编辑彭晓芸提交该选题的依据是，6月19日《人民政协报》正式发表的报道称有政协委员指出“0.4%的人占有70%的财富”。基于对政协机关报这一权威媒体的信任，时代周报编前会认可了该选题。<br />
<br />
 记者韩洪刚查实，6月19日《人民政协报》确实刊载了该报记者雷新的报道，眉题为“刘慕仁、郑楚光常委与蔡继明委员共话财富‘集中度’”，主题为“调整收入分配格局不是‘杀富济贫 ’”。报道提到――“‘我国在社会财富增长加速的同时，出现了财富向少数人手中集中的倾向。中国权威部门的一份报告显示，0.4％的人掌握了70％的财富，财富集中度高于美国。这种大部分社会财富集中在少数人手中的格局，导致了我国消费的不足，甚至产生了畸形的消费。根据调查显示，中国已经成为国际上奢侈品最大的市场。’”蔡继明委员说。(网址：<a href="http://epaper.rmzxb.com.cn/2009/20090619/t20090619_258656.htm" target="_blank">http://epaper.rmzxb.com.cn/2009/20090619/t20090619_258656.htm</a>)<br />
<br />
 6月24日，记者致电蔡继明委员求证，蔡当时正在石家庄出差，确认自己在政协会议上说过这个数据。记者追问数据来源提到的“中国权威部门”是什么部门。蔡继明表示：“是公开数据获得，具体哪里记不清了”。<br />
<br />
 至于《人民政协报》随后于7月1日重新发表声明，表示蔡继明提到的报告不是“中国权威部门”，而且国外的一份调查。但这已经是在本报文章见报之后的事情了，作为评论性文章，而非报道，本报只是基于已经见报和发生的新闻“事实”进行评论，引述权威纸媒的见报文字进行评论，这完全是国内外新闻媒体操作评论文章的可行性方法，无业务上的瑕疵可言。<br />
<br />
否则，当下所有的媒体都无法操作评论版块的文章了，因为当前国内媒体发表的评论文章，除了本报评论员，多数是学者和专业时评家，他们不是记者，写作评论文章的依据从来就是纸媒的见报文章，也无需对这些见报文章的信息进行核实，否则，恐怕没有学者和时评家敢于从事这个“危险的行业”了。<br />
<br />
五、本报《贫富分化急遽加大的危险》一文相关数据皆有权威出处，故而转引。标题制作上力求平实，并无哗众取宠之意。<br />
<br />
《时代周报》C12版舆情版属评论版块，评论选题要求是媒体公开报道的新闻事实，一般媒体并不会对此进行重复核实调查，但《时代周报》本着更加严谨认真的态度，在某些重大题材的评论均由本报评论记者操作，以便在述评中加以采访，对新闻事实和信息源进行核实。<br />
<br />
 就“贫富分化急遽加大的危险”这一题材，《时代周报》记者核实原始出处后，又在公开的权威媒体──新华网、上海证券报搜集资料，同时还补充采访了权威专家。<br />
<br />
 文中所提“亿万富翁91%是高干子弟”出处源自新华网2006年10月20日转载同日《上海证券报》A20版的文章《盛世危言：一组组令人心惊的数字背后》，作者为北京科技大学管理学院教授赵晓。该文提到：国务院研究室、中央党校研究室、中宣部研究室、中国社会科学院等部门的最新一份关于社会经济状况的调查报告，较详细地记录了社会不同阶层的经济收入：截至今年3月底，内地私人拥有财产（不包括在境外、外国的财产）超过5000万以上的有27310人，超过1亿元以上的有3220人。超过1亿元以上者，有2932人是高干子女，他们拥有资产20450余亿元。而考证其资产来源，主要是依靠家庭背景的权力下的非法所得和合法下的非法所得。(网址：<a href="http://news.xinhuanet.com/newmedia/2006-10/20/content_5227379.htm" target="_blank">http://news.xinhuanet.com/newmedia/2006-10/20/content_5227379.htm</a>)（注：新华网心虚了，这几天把文章删了，请用快照看）<br />
<br />
 91%的数据来源即上述两权威媒体的公开披露。此外，时代周报记者还通过电话采访了研究中产阶级著称的南京大学社会学院院长周晓虹教授，文中涉及周晓虹的直接引语系其观点并经其同意刊载。<br />
<br />
 六、人民网的所谓采访本报记者的新闻操作方式并不规范。<br />
<br />
7月31日下午，记者韩洪刚的确接到过人民网观点频道张玉柯的电话。他并未表示是正式采访，而说只是作为同行了解一点稿子的情况。他主要问了韩“高干子女占亿元富豪91%”数据的出处。需要强调的是，张玉柯与韩洪刚的电话沟通中，并未提及有关蔡继明教授的情况。人民网记者将网络文章视为是韩洪刚的文章，以为这个说法是蔡继明提供的说法，而“采访”韩洪刚时，韩提供的出处与蔡继明毫无关系，于是，人民网记者便想当然地以为韩记者前后说法不一致，并使用了韩洪刚“承认”这种字眼，似乎暗示韩洪刚承认文章有误，这如果不是误会，便是污蔑。<br />
