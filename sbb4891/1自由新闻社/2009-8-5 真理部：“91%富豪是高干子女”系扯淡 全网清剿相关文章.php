如此一来，这个话题变成了社会学甚至政治学课题。在一些网民的解读中，人民网进行这个调查，并不是追求新闻的准确性，而是主题先行，因为这个数据破坏了高干子女的形象，造成了不好的社会影响，所以要纠正过来。<br />
