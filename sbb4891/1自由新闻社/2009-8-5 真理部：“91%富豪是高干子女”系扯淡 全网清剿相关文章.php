高干子女财富要有一本明白账 <br />
原题：社会财富流向要有一本明白账 <br />
杨于泽 <br />
 <br />
【该文章阅读量：487次】【字号：大 中 小】 <br />
 <br />
“高干子女占超亿元富豪人数91%”一说，经由一家境外网站和一家内地媒体报道，在社会上有一定流传。昨日人民网报道称，这个数据没有权威出处，并通过专家提醒社会公众阅读此类信息时应保持一种质疑的态度。<br />
<br />
为什么有人要编造和引用这么一个数据？人民网分析称，对贫富差距加大的忧虑、对反腐败的期盼、对自己生存状态的不满，等等，都会成为促使大家关注、热议此类信息的原因。<br />
<br />
贫富差距进一步拉大，这是当下中国一个既存事实，人们自然就要谈起这种现象，进行私下或公开讨论，这就涉及到如何在数量上描述事实的问题，需要一个数据。如果有权威数据，人们自然就引用权威数据。“高干子女占超亿元富豪人数91%”权不权威呢？人们也不知道。因为缺乏数据，人们难免会把唯一的数据当成权威数据，加以引用。<br />
<br />
高干子女在超亿元富豪中的占比，其实就是社会财富的分配问题。农业、工商业生产了社会财富，而对财富的分配无论在经济生活中，还是在政治生活中，都是一个极其重要的问题。自己得多少很重要，别人得多少也很重要，有对比才有公平感。最大份额的财富被什么人拿走了，他们拿走了多大的份额，这是大家十分关心的。<br />
<br />
这个问题关涉公众的知情权，政府有告知义务，这对学术研究也是一个导向。在发达国家，此类数据不过是些普通信息，官方在统计发布，学术界在分析研究，媒体在报道评说。这样的数据和研究不应该成为禁忌和秘密。不进行研究统计是一个缺憾，研究统计了对不对公众公开，会更加令人生疑。官方的民间相关统计数据和研究的缺失，恰恰给查无实据的数据提供了空间。<br />
<br />
从政治伦理上讲，“高干子女” 有多少财产，本来就应当向社会公开。公开，有利于民众监督；不公开，其实是放任特权，给腐败以土壤与温床。公开了，“高干子女占超亿元富豪人数”跟着也清楚了，这是一种政治理性，也是一种反腐败、反特权的机制。遗憾的是，官员财产申报和公开制度在我国的推进与公众期望尚存距离，目前，新疆阿勒泰、浙江慈溪等地已进行试点，但要么申报范围和人数过小，要么申报而不公开，难言试点到位。并且根据权威调查，大多数官员对此一制度心存抵触，使得官员财产申报公开制度讨论十数年，却难以落实。<br />
<br />
今年2月，总理Wen Jiabao与网友交流时，充分肯定了这一制度；日前，新华社主办的《t望》周刊发表分析文章，称十七届四中全会有望明确该制度，中央党校主办的《学习时报》也发文建议，从新提拔干部开始公布财产，可减小官员财产申报公开阻力。这些均可视为官员财产申报公开制度全面落地的积极信号。<br />
<br />
我们正在建设政治文明，这是一个祛除公共生活神秘性的过程。有人编造“高干子女占超亿元富豪人数 91%”的数据，我们当然要揭穿它。但光揭穿还不够，我们不要给问题增添新的神秘性，引来天下人无端猜测。如果这是一个有关分配公正的问题，我们现在就来直面问题，先统计、分析和研究，再创造条件，逐步解决问题。&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; <br />
