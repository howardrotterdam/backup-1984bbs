													<h2>真理部：“91%富豪是高干子女”系扯淡 全网清剿相关文章</h2>
						
						
																											<div id="postmessage_191388" class="t_msgfont"><a href="http://politics.people.com.cn/GB/1026/9794858.html" target="_blank">http://politics.people.com.cn/GB/1026/9794858.html</a><br />
<br />
一组虚假数据是如何在网上网下以讹传讹的？<br />
――关于“高干子女占超亿元富豪人数91%”的新闻调查<br />
2009年08月05日15:38&nbsp;&nbsp;来源：人民网-时政频道<br />
<br />
　　・人民时评：吸引眼球的未必都是真相<br />
<br />
　　人民网北京8月5日电　记者唐维红 张玉珂 常红报道：近日，关于“高干子女占超亿元富豪人数91%”的信息和议论在网上广为流传。8月4日，记者在百度上输入“高干子女占超亿元富豪人数91%”这一标题，显示相关网页2810余页，而仅仅在两天前，网页的数字还是2650页。与此同时，传统媒体也陆续推出以此数据为由头的时评、专论，引起广泛关注。<br />
<br />
　　那么，这些数据从何而来？权威部门是否发布过这样的调查报告？这样的信息为何能引起如此高的关注度？带着这一系列疑问，人民网记者采访了相关人员和机构，希望通过调查揭开数据背后的真相。<br />
<br />
　　数据拼凑嫁接　信息以讹传讹<br />
<br />
　　蔡继明委员呼吁媒体准确报道<br />
<br />
　　6月26日，《时代周报》刊发了记者韩洪刚采写的报道，报道在导语中称：“在日前召开的政协十一届常委会上，中国财富的‘集中度’正在受到政协常委和委员的热切关注。蔡继明委员说：‘中国权威部门的一份报告显示，0.4％的人掌握了70％的财富，财富集中度高于美国。’”<br />
<br />
　　记者称：他“查阅了几组权威数据显示，中国财富的确在以全球最快的速度流入富人钱包。” 随后，该记者在报道中写道：“据国务院研究室、中央党校研究室、中宣部研究室、中国社科院等部门一份联合调查报告的数据,截至2006年3月底，中国内地私人拥有财产(不含在境外、外国的财产)超过5000万元以上的有27310人，超过1亿元以上的有3220人。在超过1亿元以上的富豪当中，有2932人是高干子女。他们占据了亿元户的91%，拥有资产20450余亿元。”<br />
<br />
　　这篇报道刊发后立刻被众多网站、论坛、博客，以及传统媒体转载、转贴。许多媒体和网友在转载、转贴的时候，将数据来源“简化”成蔡继明的发言，将这位曾因倡导假日改革成媒体焦点的全国政协委员再次推上舆论的风口浪尖。<br />
<br />
　　人民网记者几经周折，于7月31日终于联系上了《时代周报》记者韩洪刚。他承认，这段数据不是引自蔡继明委员的发言，而是引自互联网上转载的某位国内经济学者2006年写的一篇文章。<br />
<br />
　　随后，人民网记者又与该经济学者联系核实。该学者称此数据引自当年的互联网，他说：“当时网上（这组数据）非常流行”。<br />
<br />
　　处在舆论旋涡中的蔡继明近来不胜其扰，他曾在7月11日通过个人博客发布澄清声明，但收效甚微。这组数据仍在继续传播和热议。<br />
<br />
　　7月31日下午，蔡继明接受了人民网记者的采访。他说，事实上，他在全国政协十一届常委会第六次会议专题讨论会发言时，只是提到“国外一家研究机构估计，中国0.4%的最富裕的人掌握了70%的财富”，并没有指明是哪一家研究机构，更没有说是中国权威部门的报告。《时代周报》记者在没有对他进行采访的前提下，写了这则报道。《时代周报》并未说明后一个数据是他所说，但其他媒体再次引用《时代周报》数据时，就变成了：“蔡继明委员在近日召开的全国政协十一届常委会第六次会议上，援引一份由国务院研究室、中央党校研究室等部门一份联合调查报告的数据。”<br />
<br />
　　蔡继明强调：“我对媒体这种乱引数据，误会当事者本意的做法感到非常反感。媒体报道应本着客观、真实、准确的原则，而不是恶意炒作。”<br />
<br />
　　“权威数据”不权威　中宣部、中央党校、国务院、中国社科院等相关部门纷纷辟谣<br />
<br />
　　为了增加这组所谓数据的可信度，一些媒体在报道、转载、评论这些信息和数据时，一再强调“中国权威部门”、“全国政协委员”等关键词，可假的终究是假的。不仅蔡继明委员出面澄清事实，该“报告”列举的权威部门也纷纷通过记者正式辟谣。<br />
<br />
　　中宣部政研室有关负责人明确表示，“我室从未与国务院研究室、中央党校研究室、中国社会科学院等部门联合作过关于社会经济状况的调研，我室也从未进行过这方面的专题调研，更未起草过这方面的调查报告，在已有的调研报告中也未引用过这些数据。”<br />
<br />
　　有媒体报道称这组数字最早来自于中国社科院的“当代中国社会流动”课题。该课题负责人中国社科院研究员陆学艺对此坚决否认，连称荒唐！<br />
<br />
　　他说：“‘高干子女占超亿元富豪人数的91%’，是不可信的，因为无法统计，并且比例数还精确到个位数，很荒唐！至于说这一数据来源于‘当代中国社会流动’课题，更是荒唐！我们的课题完成于2004年，而统计数据说的是‘截至2006年3月’，这怎么可能？”<br />
<br />
　　国务院研究室、中央党校研究室相关负责人则告诉记者，根本没有这份报告，这组数据是假的，两年前曾在网上炒作过，最早来自海外某刊物。<br />
<br />
　　记者通过互联网搜索，发现早在2006年10月19日，境外星岛环球网就刊发了题为“官方报告：中国亿万富豪九成以上是高干子女”的报道。报道称：“官方研究机构的调查报告披露：在金融、外贸、国土开发、大型工程、证券五大领域中，担任主要职务的基本上都是高干子弟。中国的亿万富豪九成以上是高干子女，其中有2900多名高干子女，共拥有资产二万多亿。国务院研究室、中央党校研究室、中国社会科学院等部门近日出炉一份关于社会经济状况的调查报告，详细记录了社会不同阶层的经济收入。”这不知是不是网上最早出现的此虚假报告的来源，但此篇报道被传载、引用的频率很高。<br />
<br />
　　记者还搜索到了英文时事杂志《远东经济评论》（Far Eastern Economic Review）2007年第4期上的一篇文章，文中也提到了这些数字。文章原文为：“Article after article pores over the potential economic reasons for<br />
 the increase in income inequality in China. We ignore the fact that of <br />
the 3,220 Chinese citizens with a personal wealth of 100 million yuan<br />
 ($13 million) or more, 2, 932 are children of high-level cadres.”<br />
<br />
　　翻译成中文，大意为“一篇又一篇文章在研讨中国贫富差距扩大的潜在经济原因。但是，我们忽视了一个事实，有3220个中国人拥有财富超过1亿元，2932人是高干子女”。记者发现，该文章引用这组数据时并未注明数据来源和统计时间。<br />
<br />
　　就是这样一个被相关部门断定为造假的报告，就是这样一组语焉不详的数据，却在境内外许多网站、论坛上广泛流传，且“版本”不尽相同。有网友在此类帖文下跟帖置疑：“我希望通过数据考证来说话，不管原作者出于何种想法或意识形态写了这篇文章，我认为错误的事实都是无法支持的。”<br />
<br />
<br />
　　警惕“刻板印象”　关注舆论热点<br />
<br />
　　以权威真实的信息挤压谣言空间<br />
<br />
　　这组虚假数据的传播，让人们再次领略了互联网的传播能量和放大器的功能。<br />
<br />
　　其实，媒体尤其是互联网上统计数据混乱的现象早已有之。出自境内外民间机构、专家学者的统计数据，乃至一些拼凑捏造、以讹传讹的虚假数据，时常处于一种任意转贴、转载的状态。许多媒体和个人在转贴、引用之前，鲜有人去了解、追问这些数据的科学性和准确度。当然，普通网民个体往往很难核实，但媒体、专业机构、专业人士则不同，不能淡忘职业操守，不能以此来炒作、膨胀公众的不满情绪，更不能违犯相关法律法规。<br />
<br />
　　中国人民大学商法研究所所长、博士生导师刘俊海教授在接受人民网记者采访时介绍说，全国人大常委会6月27日表决通过的《中华人民共和国统计法》规定，民间统计调查活动的管理办法，由国务院制定。但是，这方面的规章制度目前还没有制定出来，而许多民间机构、非政府组织却一直在发布与国民经济和社会生活相关的统计结论或结果，这部分数据的真实性、完整性等，常常无法确认。<br />
<br />
　　为此，他建议应尽快出台对非官方机构统计数据的管理规定。他提出，在不损害国家安全，不泄漏国家机密，不进行欺诈欺骗，不损害社会公共利益前提下，对民间统计调查活动应允许存在，但要加强监管，加强规范。民间统计活动只要纳入法制化监管轨道，对官方统计数据也可以起到拾遗补缺的作用，促进官方统计数据的真实性、准确性、及时性，从而进一步增强官方统计数据的公信力。<br />
<br />
　　蔡继明委员在接受人民网记者采访时，也建议国家有关部门加强对统计数据的管理。他还一再强调，国家有关部门应及时对境内外民间机构和组织等发布的不实数据进行澄清，以免给其留下传播空间。<br />
<br />
　　记者在调查中也注意到，许多网友在关注、甚至转贴此类信息时，并不完全相信这些内容。一位网友就明确表示：“网上传播的这些信息，包括高干子女占超亿元富豪的比例等等，其数字不一定很准确，但收入差距过大以及特权阶层掌握了过多的社会财富，却是不争的事实。”<br />
<br />
　　可以说，对贫富差距加大的忧虑、对反腐败的期盼、对自己生存状态的不满，等等，都会成为促使大家关注、热议此类信息的原因。<br />
<br />
　　前不久，《第24次中国互联网发展状况统计报告》发布，中国网民已达3.38亿，稳居世界第一。一个超过3亿人的群体不再虚拟，他们常常通过发贴、开博、留言等方式表达诉求、宣泄情绪，而用数据说话，正是表达最常用的手段之一。<br />
<br />
　　国家行政学院教授、博士生导师刘熙瑞在接受人民网记者采访时就谈到了此类虚假信息传播背后的原因。他说，“这类虚假数据之所以在博客和论坛广为传播，大多是网民们的一种情绪宣泄，或者作为他们一种价值理念的传播。”他认为，尽管这种信息缺乏根据，但是导致此类信息如此广泛传播背后所隐藏的群众的某种不满情绪应引起相关部门的重视。刘熙瑞建议，各级政府及有关部门在依法整治网上网下虚假、有害信息的同时，也应重视这种现象，分析、研究这种现象背后的深层原因，并采取有效措施予以解决。<br />
<br />
　　中国人民大学新闻学院副院长、舆论研究所所长喻国明在解读此类现象时，使用了传播学的一个概念――“刻板印象”。他说，一个真假并无确认的数据之所以被广泛流传，甚至为不少人认可，形成共震，是因为社会上有大量的类似这样事件的逻辑积累，传播学称之为“刻板印象”。<br />
<br />
　　他认为，社会贫富差距已成为目前影响社会稳定问题的一个重要因素。在现实生活中，一方面少数人利用公款大吃大喝，另一方面还有很多人处在生活的困境中。在这种情况下，这方面的虚假数据就很容易给人们造成一种逻辑上的真实。其实，这正是社会的一种预警信号。他认为，政府部门应针对这些预警做出相应的政策性的调整和安排，切实解决民众关心的热点问题；此外，还应给公众创建一种情绪宣泄的平台，使这种社会情绪得以舒解。<br />
<br />
　　在采访的最后，喻国明特别强调，媒介使用相关数据时，应该有严格的核查意识。如果数据信息没有权威解读而又未得到核实时，应注明这是未经核实的数据，提醒社会公众阅读此类信息时应保持一种质疑的态度。<br />
（责任编辑：郭亚飞）</div>

							
							
							
							
