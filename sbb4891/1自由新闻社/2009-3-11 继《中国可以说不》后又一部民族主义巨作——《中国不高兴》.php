【书摘】<br />
<br />
 <br />
<br />
 <br />
<br />
▲&nbsp; &nbsp;&nbsp; &nbsp; 其实这里隐含着一个潜在的逻辑，就是大人没有把事情做好，结果大人要用小孩的未来为自己的疏忽买单的问题。……在金融危机后，很多政府官员忙着讨论如何到华尔街挖“金融人才”的时候，网上的年轻人却在热烈讨论甘肃星火机床公司用200万欧元收购法国索玛数控机床公司81%股份的事情，这对中国突破薄壁结构、形状复杂的航空、潜艇发动机加工技术等瓶颈意义十分重大。有意思的是，这些年轻人几个月前可能就是在网上号召抵制家乐福的年轻人……可见，事情并不是上一代人凭借自己的历史记忆想象的那么简单。（宋晓军，《事情正在起变化》）<br />
<br />
 <br />
<br />
▲&nbsp; &nbsp;&nbsp; &nbsp;奥运会之后的三鹿奶粉事件对中国人的爱国心打击非常大。上级领导最喜欢的是“鸟巢一代”，既爱国又听话，“火炬一代”（其实和“鸟巢一代”是一代人，但不一定是同一群体）可能不够听话，不够驯服。三鹿奶粉事件一出来，民意摆动非常大，“火炬一代”中的很多人就反弹了，很气愤，说：“我们三四个月前上街很傻。 ”这件事充分反映出现阶段的精英腐败，给中华民族凝聚力带来巨大的问题。（王小东《耳光能把我们掴醒吗》）<br />
<br />
&nbsp;&nbsp;<br />
<br />
▲&nbsp; &nbsp;&nbsp;&nbsp;中国一定要有这样的视野，中国人应该给自己设立这样一个目标。即使当下这个并不处于巅峰状态的中国，也具有很大的力量和足够的财富，但就是不知道干什么，觉得没有什么好干，于是就腐朽就堕落，拿着力量腐朽和堕落。一个国家、一个民族应该有大目标。有了这个大目标，大家为之奋斗，这个国家、民族才有希望，内部的人群才会有道德，才会有诚信，才会有好的行为。（王小东，《走一条人类历史上从未有过的路》）<br />
<br />
 <br />
<br />
▲&nbsp; &nbsp;&nbsp; &nbsp; 现在学者给自己报课题，厂家给自己报项目，全都一副老虎吃天、崩爆米花的架势，可一到中华民族的百年大计、千年伟业，一个个比着低声细语，生怕吵了外国友人的午休。其实以中国的人口规模、历史规模和文明规模，我们只要继续生存，就必然要参与创造世界历史，我们面前只有两条路：或压垮这个世界，或再造这个世界。这是个比近代基本任务还要大得多的工程，应该有个时间表或路线图，哪怕是草图。同时，我们也要分出近期任务和长期任务。（黄纪苏，《2008，神鬼莫测》）<br />
<br />
 <br />
<br />
▲&nbsp; &nbsp;&nbsp; &nbsp; 关于西藏是不是自古以来就是中国的领土，到底是元朝还是清朝，还是1959年？西方人硬说是1959年……我也可以明告诉你们这些西方人，就是1959年又怎么样？你有种你过来打，你废什么话？……还有一个说法，说过去可以抢，现在不可以抢。这是谁定的？美国在名义上没有吞并国土，但事实上行的是灭国之战，又有谁吭气了？其实，就像网上的愤青说的那样，咱们要是讲道德，你们就全滚回欧洲老家去。我们道德上没有亏欠！这就是中国年轻人的回答，民间的回答。（王小东，《到了该由西方正视中国“不高兴”了》）<br />
<br />
 <br />
<br />
▲&nbsp; &nbsp;&nbsp; &nbsp;最野蛮的种族主义分子炮制出的下流文字，就这样在兴而未艾的国民劣根性讨伐中借尸还魂，“弱者、愚笨者的繁殖都是最快的”――这就是邪恶的愤懑主义对中国民族品质和民族现实的最终诊断。表面上看起来，这是一个极端的案例，然而，所有的看似怪诞的情绪的源头就在此。（宋强《这个时代的心理性悲剧》）<br />
<br />
 <br />
<br />
 ▲&nbsp; &nbsp;&nbsp;&nbsp;中国的发展，已经到了一个统计文化决定统治文化的时代，大大小小的群体性事件，大量出口企业的失业农民工，这些现实问题是必须要有一个大目标才能解决的，或者说有了大目标是解决成本最低的。这个大目标实际上就是两手准备，一是将依附、跟进为主的经济发展模式转化为自主、创新为主的模式，二是为了防止人家用战争阻止这种转化，我们必须尽快发展可以强化军事实力的产业。（宋晓军，《中国无法不显其大》）<br />
<br />
 <br />
<br />
&nbsp; &nbsp; ▲&nbsp; &nbsp;&nbsp;&nbsp;这么大一个国家，至少应该是打商战，而打商战一定要有军事装备做后盾，而军事装备一定要有产业做基础。这么简单一个逻辑，年轻人难道看不出来吗？所以如果没有“大目标”，对年轻人的所谓理想教育是混不下去的。（宋晓军：《美国不是“纸老虎”，而是“老黄瓜刷绿漆”》）<br />
<br />
 <br />
<br />
▲&nbsp; &nbsp;&nbsp;&nbsp;俄罗斯最清楚美国是老黄瓜刷绿漆：你是老黄瓜我清楚，你刷点绿漆，在我家门口你也不敢夯，咱们找一个居中的地方我可能练不过你，但是近了你肯定没戏。中国不是这样，90年代之后学人家，之后就开始采用铺天盖地的信息战，买无数的电脑，看演习画面全是信息战，你机械化还没完成呢，“信息至上”有点赶早了吧？玩信息战不能替代机械化。信息战只是把机械化的能量稍微发挥大一点，并不能决定战争的胜负，被美国误导，也跟着“新黄瓜刷绿漆”，你就无所作为吧。（宋晓军：《美国不是“ 纸老虎”，而是“老黄瓜刷绿漆”》）<br />
<br />
 <br />
<br />
▲&nbsp; &nbsp;&nbsp;&nbsp;要正视这样一种严峻的形势，中国在和平崛起的道路上，“有条件决裂”（像英国人曾经说的“光荣孤立”）可能是一个充满困难的选项，却是一个具有可能性的选项。中国对于这种前景应该做怎样的应对？一旦和谐相处不可能，我们有无准备，包括国家出现最困难的局面的准备？（宋强，《中国可以不说》）<br />
<br />
 <br />
<br />
▲&nbsp; &nbsp;&nbsp;&nbsp;中国要有大目标、大抱负，而不是小吟味、小情调。中国的精英，尤其是政治和文化精英，应该建立起这个自觉。个人的崛起要搭乘民族的崛起才事半功倍。个人跟民族的关系是风筝跟风的关系。如果民族的天风浩荡，风筝一下子就上去了。天风没有，您拽根小线跑得上气不接下气，也只能跑出个相对二三级风来，风筝也就五六米高。所以对我们来说，就要立中华民族的大志，要对人类做更大贡献！（黄纪苏《大目标从哪里诞生》）</div>

							
							
							
							
