<br />
流行于1967年的一种保健术。前后历时大约10个月左右。各地流行（从大都市北京到穷乡僻壤的西双版纳）的起讫和流行的具体时间、长度略有不同。方法是抽取小公鸡（也有说4斤以上重的纯种白色“来航鸡”最好）的鸡血几十到100毫升，注射进人体，每周一次。<br />
<br />
据传[谁？]，这是1965年流行于某省“上层社会”一种的时髦。其“缘起”是，某国民党中将军医被中共公安机关抓获判了死刑，行刑前献出这个“秘方”以求自保。称，其疗效可以强身健体，延年益寿，治愈百病；在台湾的蒋介石就靠鸡血活着。不但当地行政首长依法办理，还有一帮效仿的下属。文革中该首长被批斗，在催逼追问下坦白了打鸡血的事情。于是被当作“延年益寿，抢班夺权，复辟资本主义”的罪行，深揭狠批，并印上传单，广为流传。造成了打鸡血的大流行。<br />
<br />
据考[谁？]，鸡血注射在皮下肌肉（而非静脉注射）里，液体蛋白进入人体所引起的免疫反应，对某些特殊疾病可能有一定疗效。一些人[谁？]有进补后的感觉，浑身燥热，脸色红润。在医学、医药条件相对低下的当时，可能具有某种类似替代性作用。但它的疗效和适用范围，显然被极端夸大了（一种夸张说法称它对半身不遂、脑中风、妇科病、阴道搔痒、不孕症、牛皮癣、脚气、脱肛、痔疮、咳嗽、感冒等都有治疗和预防的作用）。而且由于鸡感染的疾病较多，鸡血的质量情况也不明确，带来副作用的可能性不易控制（也有人注射后病亡的案例）。鸡血疗法本身的科学性没有得到理论和临床的证实，使用它的除了个别正规医院外，多是城市的街道诊所、县乡医院。高潮时候还需要排队注射。 一时间竟造成了小公鸡因紧俏而涨价，在低工资、高就业、物价稳定的当时，成为极罕见的现象。据称，被抽过血的公鸡瘦骨嶙峋，烹饪、食用时没有香味。 文革激发的非理性认知模式，使中国老百姓在作为原始信仰的血液崇拜面前，产生认同感、亲和感。打鸡血的流行便显得顺理成章了。大量非正常死亡、流血，使人们意识到生命的脆弱、易逝，因此特别重视肉体生命的保全。只是借助于西洋注射器械，又有了些“洋为中用”的科学色彩、现代意味，似乎愈发增加了可信度；在从众心理的驱使下，一哄而起、应和者特别地多。1980年代，相声演员姜昆曾创作《红茶菌和打鸡血》对其进行讽刺。<br />
