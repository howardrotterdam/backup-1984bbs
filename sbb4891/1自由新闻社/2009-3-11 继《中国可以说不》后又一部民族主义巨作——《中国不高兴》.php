													<h2>《中国不高兴》内容简介</h2>
						
						
																											<div id="postmessage_53120" class="t_msgfont">宋晓军<br />
<br />
（1）持剑经商：崛起大国的制胜之道<br />
<br />
　　中国需要一群英雄，一个真正的英雄集团。人数不能太少，因为一两个人不能解决问题。我们需要这样一个英雄集团带领我们这个民族，完成在这个世界上管理、利用好更多的资源，并且除暴安良的任务。我们要有制度建设，也要有文化建设。文化建设，就是尚武精神。<br />
<br />
（2）“文艺腔”不是我们今天应该玩的<br />
<br />
　　“文艺腔”的另一个大问题是把“文化”，把“软实力”放到了过高的位置。这些年来，我们不断地看到思想界、学术界、主流媒体，乃至跟着鹦鹉学舌的政界、商界，没完没了地强调“文化”的重要性，强调“软力量”的重要性，甚至强调仅凭所谓“中国传统文化”就可以感化西方人，“为万世开太平”。然而，强调了半天，中国的“软力量”仍旧弱到几乎是负数。<br />
<br />
（3）切勿去学香港“管家文化”<br />
<br />
　　50年后人们再回头来看，中国今天学术思想的贫乏，就会看得很清楚。这几十年中，中国压根就没人，没有像样的作家，没有像样的思想家。钱钟书，韩寒，算个什么？他们被吹成这样，其实没有任何像样的原创性东西，最多耍一点小聪明！中国的学术界、文化界在精神上跪着，怎么可能出现原创性的人物呢？原创是需要站着的。<br />
<br />
（4）解放军要跟着中国核心利益走<br />
<br />
　　未来解放军的任务绝对不是现在说的国土防卫，而是应该跟着中国的核心经济利益走，中国核心经济利益到什么地方，解放军的力量就应该覆盖到什么地方。现在覆盖不到，是现在做得不好、不够，要努力改进。<br />
<br />
（5）美国不是纸老虎，是“老黄瓜刷绿漆”<br />
<br />
　　2008年8月关于格鲁吉亚和俄罗斯冲突，我（宋晓军）在凤凰台做了一期《锵锵三人行》的节目。做节目前，窦文涛在那化妆，问咱们怎么说，我说就说老黄瓜刷绿漆，说太专业的军事技术没有意义。他挺好奇，问这是怎么回事。我说：从军事上看，俄罗斯就是老黄瓜没刷绿漆，美国人是老黄瓜刷了绿漆，其实在本质上都是老黄瓜，半斤八两。<br />
<br />
（6）历史会不幸证明，奥巴马拯救不了美国<br />
<br />
　　美国的问题是不那么容易解决的，谁当政都不可能轻易解决，但奥巴马摇滚歌星式的执政方式是更不行的。他执政不如希拉里、麦凯恩，乃至小布什。有人也许会说，美国的政治制度好，能够制衡一个没有执政经验和智慧的总统，甚至能够制约一个胡来的总统。但这样一来，美国所谓的“变革”也就成了胡扯了。<br />
<br />
（7）金融危机的缘由：不干活想住大房子<br />
<br />
　　实际上，次贷危机也好，美国贸易赤字也好，说穿了，就是美国人消费得太多，生产得太少，形成了缺口。这个缺口怎么补？一是抢，二是骗。<br />
<br />
　　这次金融危机体现了美国社会从上到下的全面腐朽。现在美国老百姓群情激愤，都说这次危机是华尔街那帮混蛋和小布什政府的错。但是我们平心而论，美国老百姓又怎么样？你活干得这么少，压根就没那份钱，你凭什么住大房子？<br />
<br />
（8）中国对西方：“有条件地决裂”<br />
<br />
　　毋庸讳言，近30年来，我们处于一个长期被遮掩的真相中。中国人以最大的热情欲图拥抱西方，以最亲善的姿态告诉西方：“我们在向你们靠拢”，而西方的回答是：“你们在哪里？”自我矮化的时代歧路，绝不是心理镜像，而是周遍都存在的活生生的现实。<br />
<br />
（9）马立诚等“优秀的中国人”的“勇敢”是在挑战民族底线<br />
<br />
　　马立诚等人的“勇敢”是在挑战民族底线，在中国并不积弱的年代，他们的这一番表演，很精准地刻画了精英是怎样制造堕落的“时代精神”图谱的。如果他们不加检点，任由自己高蹈下去的话，未来的汉奸排行榜上，会少一些争议，少一些曲笔解读。<br />
<br />
（10）成熟的大国心态不光是“宽容”，也要较真儿<br />
<br />
　　在中法关系上，就是要明确“惩罚外交”的概念。惩罚，报复，这是国际间交往的常态。<br />
<br />
　　说到萨科齐的表演，他是有着明显生物性的，他的翻云覆雨，看起来有滑稽性的特点。<br />
<br />
　　在一些不友好国家轮轴闹的情势下，我们不能轮盘赌，不要把外交思路弄得那样零碎，把法国归于“差信誉客户”，着着实实惩办一回。搞一点“解气外交”、儆尤外交，也符合我们开放时代的大思路，成熟的大国心态不光是“宽容”，也有较真儿。<br />
<br />
（11）学术腐败如今已严重到了没人拿它当回事的程度<br />
<br />
　　由于学术的腐败，读书人越来越没读书人的样子，一个个看着像官场上的小秘书、市场里的小商贩。<br />
<br />
　　从历史上看，读书人怎么也都还算是社会中比较健康的力量，带着棺材上任，国亡自沉的可不尽是读书人么？相对于其他群体，读书人成天“上下古今”，本来最有可能超越自身经验和利益，最有可能截断恶的链条，打破堕落的循环，成为前面说过的、社会改良的启动基金。但今天，你看他们或一头扎当权者怀里，或歪坐在资本家腿上，或一人一夜、被俩主儿轮包。<br />
<br />
（12）大时代应有大文化<br />
<br />
　　树立大抱负、提升软实力、改良社会、重建人心、促进文化艺术的产品升级，应该是未来若干年中国社会特别是文化思想界的一个重要任务。现在这拨所谓的大腕，他们占据着最大的资源，但凡做出一点垃圾来，就通过强大的资金和权力进行媒体运作，忽悠全民认购。中国文化要真正复兴，这些文化艺术的“领军人物”要么改邪归正，要么下岗出局。他们是这30年的社会文化、社会心理的产物，他们已经完成历史使命了。中国不往前走则已，中国的文艺不向上走则已，要往前向上，就凭这些人，凭他们这副败家丧气的样子，走在队伍前头肯定是要耽误事的。<br />
<br />
（13）再不抛弃洋奴文化，我们就没得救了<br />
<br />
　　当人们说中国的污染极为严重的时候，是谁把中国变成美国的加工厂，以至于中国制造得越多，污染越严重，美国越赚钱？当中国人辛辛苦苦赚了一点钱，是谁又让中国人用几亿件衬衫去换一架美国飞机，却不愿对中国自己发展大飞机进行投资？当中国积累了一些外汇储备，又是谁将这些外汇变成巨额美国国债，让中国穷人把血汗钱借给美国富人？还合演了人民币对美元单独升值的“无奈”？并且在美国金融危机中让中国的财富受到重大损失？<br />
<br />
（14）建立英雄集团从逼迫内部一点点高尚做起<br />
<br />
　　如果把世界资本主义体系比作一个拳坛的话，我们近期中期目标就是打 倒拳王，终极目标是打碎拳坛。终极目标当然不在我们眼前，但应该在我们心里。不妨把未来的理想社会当个存钱罐，平时有点社会实验、人生探索什么的，就当毛票钢蹦塞进去慢慢积累吧。有了这一路排下去的大任务大目标，一个民族就有事干了，就不至于醉生梦死、行尸走肉了。<br />
<br />
（15）放下小菩萨，塑伟大之目标<br />
<br />
　　中国要有大目标、大抱负，而不是小吟味、小情调。中国的精英，尤其是政治和文化精英，应该建立起这个自觉。精英无精打采腐朽成这样，说明既有的目标该调整了。不调整振奋不了精神，进入不了状态，凝聚不了力量。中华民族在世界文明史上还需要跨出一步，她需要动力，动力来自目标。<br />
