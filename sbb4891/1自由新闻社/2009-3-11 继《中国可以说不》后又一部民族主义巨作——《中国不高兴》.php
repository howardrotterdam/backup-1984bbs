																											<div id="postmessage_50527" class="t_msgfont"><a href="http://blog.sina.com.cn/s/blog_473f26830100dxr0.html" target="_blank">http://blog.sina.com.cn/s/blog_473f26830100dxr0.html</a><br />
<br />
&nbsp; &nbsp; 几个“新左派”高高兴兴合著了一本书，叫《中国不高兴》，出版了。还没看见实体书，因为我一定要读的，所以事先预习一下目录――从宋强那里偷来的目录――他是这本书的作者之一，从他几个月前用挑衅的口气告诉我要出版这样一本书的时候，我就在等着它。<br />
<br />
<img src="http://i41.tinypic.com/nvxxfl.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /> <br />
<br />
 <br />
<br />
&nbsp; &nbsp; 光看目录，或者光看作者，我就知道我对他们的某些观点以及观点所赖生的思想平台会有邪念，但我一直相信他们特有的热情和理性，他们身上，在很大程度上，很好地保留了上世纪老愤青们的优良品质，这也是我尊重新左派而蔑视一根筋左粪的理由，一根筋左粪大部分是马桶脑袋的机器变形人，只有观点没有思想，只有行动没有路线。同样，我对于正规右派以及迂腐右粪的感情，与此类似。一言蔽之，我对中国的左右派都是尊重的，我不爽的是他们中间的偏激分子，偏激分子们是供我娱乐的“小沈阳”，因为单纯和固执，有时候他们显得很可爱，但更多的时候，是可笑，一直相信粪粪们是被打了激素的，左粪打屁股，右粪打脑袋。<br />
<br />
&nbsp; &nbsp; 新左派和理性右派的可爱处，在于他们都相信自己兜里的东西更适合中国，虽然他们对国家以及与国家、民族有关的概念的理解上经常有较大的偏差，但这两个交错矛盾着的群体，同属于一个焦躁着的天真且悲哀的知识分子群体，他们往往怀抱美好的理想，企图为公平和正义代言，从不同的角度对时政采取批判的态度，梦想按照自己的理想改造（右）或改良（新左）社会。其实一切都是不可能的，知识分子从来没有完成过这样的梦想，普遍意义上的知识分子不是行动家更不是革命家，他们唯一的使命仅仅是“启蒙”社会，启蒙之后还要没完没了地张扬，往往就要走进散布流毒的歧途。<br />
<br />
&nbsp; &nbsp; 民智既开，群众具备了思索人生、关照社会的素质，也明白了自己应该具有选择未来的权利，这时知识分子的任务，是努力走在群众之先，提前一步看到未来的各种可能性并提供预警。那么今天的知识分子是不是该履行这一指责了呢？还没有，因为中国的思想启蒙运动总是不断被强力的权势所中断阉割，每一项启蒙都是残废着的，事实上，连知识分子自己也没有被很好地启蒙成功，说白了，其实很久以来都是一群糊涂蛋在煞有介事地思索中国的未来。<br />
<br />
&nbsp; &nbsp; 说到这里，算是先入为主地对这本书的评价了，之所以这样伤人的话也要讲出来，是为了帮助宋强们抵挡一下马上要从右边飞来的板砖以及泼出的大粪，右派和右粪们大可不必为这样一本书愤怒，因为和所谓左派一样，所谓右派，只不过是另一群糊涂蛋思考者而已，糊涂蛋大战糊涂蛋，无非娱乐了我们这些自由派或骑墙主义者的看客。<br />
<br />
&nbsp; &nbsp; 积极的反应是：不论门派何出，关心国家民族之命运的人们都能认真地思考着整体的将来，各抒己见，理性争鸣；当然，执政者也要肯于放开心胸、降低底线，四面墙开，才能四面花开。果能如此，《中国不高兴》这本书即便真的难免主观和派性，也将起到抛砖引玉的效果，又怎么能说不美好呢？<br />
