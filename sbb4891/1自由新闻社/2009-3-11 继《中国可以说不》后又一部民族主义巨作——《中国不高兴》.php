													<h2>《中国不高兴》之重磅观点</h2>
						
						
																											<div id="postmessage_52252" class="t_msgfont">1.英雄国家：每一个中国人都应该具有的心理指标<br />
<br />
 我们中国需要一群英雄，一个真正的英雄集团。多少人我不知道，总之人数不能太少，我不相信一两人就能解决问题。我们需要这样一个英雄集团带领我们这个民族，完成在这个世界上管理、利用好更多的资源，并且除暴安良的任务。我们要有制度建设，也要有文化建设。文化建设，就是我说的尚武精神。<br />
<br />
2.再不建立大目标，中国就没机会了<br />
<br />
 <br />
<br />
中国应该有什么样的大目标？我认为第一是要在这个世界上除暴安良，第二是要管理比现在中国所具有的更大更多的资源，给世界人民带来福祉。<br />
<br />
有人说中国应该给世界提供一个榜样，但我认为，如果中国只是要给世界提供一个新的榜样，那这个目标太小了，中国应该提供的是真正的管理和领导。……如果中国人当中能有一部分人有一个大目标，并为之努力奋斗，中国必能更快地改正自己的许多缺点，重新站到领导这个世界的位置上。如果中国在这个位置上，至少不会像今天的美国那样：好吃懒做、不负责任，堕落到抢、骗的地步，而使整个世界陷入经济衰退当中。<br />
<br />
3.解放军要跟着中国核心利益走<br />
<br />
 未来解放军的任务绝对不是现在说的国土防卫，而是应该跟着中国的核心经济利益走，中国核心经济利益到什么地方，解放军的力量就应该覆盖到什么地方。现在覆盖不到，是现在做得不好、不够，要努力改进。<br />
<br />
 4.火炬在法遇阻，萨科奇见达赖，在对外关系上中国“有条件地决裂”<br />
<br />
在中法关系上，就是要明确“惩罚外交”的概念。惩罚，报复，这是国际间交往的常态。<br />
<br />
说到萨科齐的表演，他是有着明显生物性的，他的翻云覆雨，看起来有滑稽性的特点。<br />
<br />
在一些不友好国家轮轴闹的情势下，我们不能轮盘赌，不要把外交思路弄得那样零碎，把法国归于“差信誉客户”，着着实实惩办一回。搞一点“解气外交”、儆尤外交，也符合我们开放时代的大思路，成熟的大国心态不光是“宽容”，也有较真儿。<br />
<br />
5.不能听“金融战士”忽悠，制造业才是正途<br />
<br />
 曾几何时，在中国这样一个说法流行了起来：制造业是低智商、低层次的人干的，其结果是费力而好处全被别人拿走；真正高智慧、高层次的人是从事金融业，打赢金融战争。这样的代表就是宋鸿兵的《货币战争》。他们会举出种种数据，说明中国从事制造业是多么地“亏”。就经济发展战略，乃至人们的价值导向而言，再没有比这种说法更误国误民的了。<br />
<br />
 6.一语道破金融危机的缘由：不干活想住大房子<br />
<br />
 其实美国金融危机的缘由很简单，不像那些故弄玄虚的人说的那么复杂。……我认为这次金融危机体现了美国社会从上到下的全面腐朽。现在美国老百姓群情激愤，都说这次危机是华尔街那帮混蛋和小布什政府的错。但是我们平心而论，美国老百姓又怎么样？你活干得这么少，压根就没那份钱，你凭什么住大房子？<br />
<br />
 7. 产业升级的最不利条件是我们自己的心理障碍<br />
<br />
今天我们实现产业技术升级的最大障碍、最大不利条件，恰恰是我们自己的心理障碍，我们的精英被西方人吓破了胆。<br />
<br />
以中国人的聪明才智和丰富的人力资源，只要打破那种认为我们搞科技不行的心理障碍，我们应该不仅能够跟踪西方最先进的科技发展，而且还能做一些因需要大量的人力资源而西方做不了的事情。<br />
<br />
 <br />
<br />
8.持剑经商：崛起大国的制胜之道<br />
<br />
 <br />
<br />
经济利益的获取，或者说，想弄钱，无非就是生产、抢劫、欺骗，没有别的招，物质守恒嘛。在这几招里面，生产为上策，拿剑保护着生产为上上策。……无论如何生产都是第一位的，但一定要有剑，剑和商要结合，单独生产是不行的，最好剑和商有一个平衡，就是持剑经商，这是上上策。<br />
<br />
 无数的事实已经证明，西方几百年来形成的“持剑经商”的传家宝是不会丢弃的，你想靠自己刀枪入库、马放南山感化人家放下手中的剑，跟你温和地做买卖，这可能吗？这其实是机会主义！<br />
<br />
 9.西藏问题的核心是主权问题<br />
<br />
 <br />
<br />
中国一些知识分子，……说过去可以抢，现在不可以抢。这是谁定的？美国在名义上没有吞并国土，但事实上行的是灭国之战，又有谁吭气了？其实，就像网上的愤青说的那样，要是你们从澳洲、南北美洲、非洲、亚洲全卷了铺盖，我们就从西藏卷铺盖。咱们要是讲道德，你们就全滚回欧洲老家去。我们道德上没有亏欠！这就是中国年轻人的回答，民间的回答。<br />
<br />
10.摇滚歌星式的“变革”，历史会不幸证明，奥巴马拯救不了美国<br />
<br />
 今天美国的问题和罗斯福时代大有不同：罗斯福时代的美国，生产能力极强，确实就是一个生产过剩，有效需求不足的问题，而今天的美国，本来就欠着债呢，不是有效需求不足，而是本国生产能力根本就满足不了自己的消费欲求的问题。用同样的药方治完全相反的病症，我看要出更大的问题。<br />
<br />
11.切勿去学香港“管家文化”<br />
<br />
 50年后人们再回头来看，中国今天学术思想的贫乏，就会看得很清楚。这几十年中，中国压根就没人，没有像样的作家，没有像样的思想家。钱钟书，韩寒，算个什么？他们被吹成这样，其实没有任何像样的原创性东西，最多耍一点小聪明！中国的学术界、文化界在精神上跪着，怎么可能出现原创性的人物呢？原创是需要站着的。<br />
<br />
 <br />
<br />
香港“管家学术”本来是最糟糕的一种学术，现在大陆都学了这个。高校老师写的论文，99%全都是废话，什么意思都没有，生造了一堆不沾边的概念，然后把这些概念这么摆那么摆，绕来绕去，排列组合，就算是“论文”了。<br />
<br />
 <br />
<br />
 <br />
12.“文艺腔”对中国现实的戕害<br />
<br />
“文艺腔”不仅成为了官方回避中国在现代化过程中逐渐暴露出来的与西方的结构性矛盾，也成了在野知识分子实现自己“政治抱负”的一种表达方式，而且这些年越来越明显。<br />
<br />
“文艺腔”的另一个大问题是他们把“文化”（看上去比“文艺”广义一点），把“软实力”放到了过高的位置。这些年来，我们不断地看到思想界、学术界、主流媒体，乃至跟着鹦鹉学舌的政界、商界，没完没了地强调“文化”的重要性，强调“软力量”的重要性，甚至强调仅凭所谓“中国传统文化”就可以感化西方人，“为万世开太平”。然而，强调了半天，中国的“软力量”仍旧弱到几乎是负数<br />
<br />
 <br />
13.揭穿“逢中必反” 的愤懑主义本质<br />
<br />
 愤懑主义，普遍的实际效果，就是仅仅教谕人怎么去绝望，去助长怀疑和不信任的气氛；愤懑主义表面上做出一副占据了道德制高点的样子，其实细分析一下，这一点也是大成问题的，甚至从一般社交意义上说，他也是缺乏友善的。因为愤懑的劲头儿上来，人的状态是赖了吧唧的，认定了自己的信息是最充足的，智力是最卓越的，认定了所有持有不同反应态度的人都是愚民。因此，即使造成了不愉快，他也不会去省察究竟为什么。<br />
<br />
 永远“清醒”而对立的“本土愤懑”，热衷于以愤懑制造绝望，拆穿了，就是把一切值得愤恨的对象，把一切谴责的议题，引导向“逢中必反”的决然对立，对立了又看不到他满意的效果，于是乎虚无主义，于是乎恶念膨胀。这不是什么新鲜玩意儿，不过是上世纪80年代的自虐史观在借着其他的壳延续着而已。</div>

							
							
							
							
