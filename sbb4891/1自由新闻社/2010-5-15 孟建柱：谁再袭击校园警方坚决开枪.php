													<h2>孟建柱：谁再袭击校园警方坚决开枪</h2>
						
						
																											<div id="postmessage_616786" class="t_msgfont">孟建柱率员督查 谁再袭击校园警方坚决开枪<br />
<br />
2010年05月15日10:07<br />
<br />
<img src="http://images.beijing2008.cn/20080504/Img214338238.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　近期数起伤害学生和幼儿园孩子的恶性案件,令人震惊。昨日,公安部办公厅副主任、新闻发言人武和平说,国务委员、公安部部长孟建柱已率员到基层检查校园安全;公安部派出18个督导组,到各地督导各项防范措施落实;公安机关将使用警械枪支“坚决执法、毫不客气”打击危害校园安全犯罪。<br />
<br />
　　私立学校也将彻底检查<br />
<br />
　　武和平说,目前公安部已把落实校园安全的重点放在乡村、街道、城乡接合部、偏僻地区一些安全防范最薄弱的学校、幼儿园;同时对一些存在安全隐患的私立、私办的学校、幼儿园、托儿所以及各类课外青少年的校外活动场所要实行拉网式的检查。<br />
　　武和平说,案件发生后,警方坚持严打方针,“通过严厉打击和果断制止,要打到犯罪分子不敢对孩子下手,要通过严密的防范和防控,防到犯罪分子不能对孩子下手”。<br />
<br />
　　谁胆敢再犯警方可用枪<br />
<br />
　　武和平称当前“危害校园和幼儿园秩序”、“侵害师生安全甚至于剥夺师生生命”的暴力犯罪“丧尽天良”,“必须以严厉的手段来进行惩治”。他表示,不管什么原因,采取残暴的、灭绝人性的、轻易剥夺他人生命的行为,都必须受到法律严惩。<br />
<br />
　　武和平同时介绍,公安机关和司法机关依照法律对这种犯罪行为进行严惩,树立依法治国的权威,震慑犯罪分子使其不敢以身试法;“如果犯罪分子胆敢再采取这种手段,我们将依据刑法的有关规定和人民警察使用警械和枪支的规定,坚决执法,毫不客气”。<br />
<br />
　　柔性措施<br />
　　关心弱势群体排解矛盾<br />
<br />
　　武和平表示,作为治安问题,不能单纯从办案角度去解决,社会各个方面各种力量要一起动员,实行综合治理。<br />
　　一是通过排查化解在基层出现的矛盾,给弱势群体以关心、关怀,及时解决他们的困难。<br />
<br />
　　二是对于一些思想偏激、性格偏执的人员进行必要的心理疏导;对有可能造成危险的人员要通过群众的举报采取必要的救治和管理措施,更重要的是能够深入到群众当中去,来加强基层基础工作,了解社情民意,使社区成为社会管理的一个最基础的细胞,各种手段综合使用,使和谐因素尽可能增大,把消极因素解决在萌芽状态。<br />
