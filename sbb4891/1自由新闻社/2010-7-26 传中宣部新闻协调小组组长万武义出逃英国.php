在新华分社办公兼宿舍楼的一个单元房里，我们见到了万武义。他躺在一张颇为简易的床上。看上去比较瘦弱。<br />
<br />
网上有报道说，“万武义随中组部一个团访问英国。他说腰痛病复发，不能走路，代表团就把他留下。到目前仍未归队”。<br />
<br />
万武义在接受BBC中文网采访时说，他之所以滞留英国无法返国的确是因为腰椎间盘突出的老病复发。<br />
<br />
万武义说，他5月22日按照中组部的统一安排到英国牛津大学参加一个城市应对危机培训班。原计划6月6号培训结束后回国。但6月3号腰病突然复发，无法行走，卧床50余天，一直在牛津的一家骨科医院（St.Luke’s hospital）医治，现在已恢复到80%的程度。不过万武义强调说，他实际上是“归心似箭，巴不得明天早上就能回国，回家”。<br />
<br />
万武义还表示，他并非如网上所说是“擅自出走”。而是有“书面的请假信”。新华社领导还专门作了批示，要求新华社伦敦分社尽其所能，找医找药，并要求万武义“安心养病”。<br />
<br />
近日中共出台防腐新规定，要求官员报告家庭成员移居海外等情况。此举是为了整治所谓的“裸官”。因此网上传出万武义妻子先其已在英国，他去会合的消息便格外引人注目。<br />
<br />
在采访中，我直接了当的问万武义是否他夫人已在英国，他到英国来同夫人会合。于是就有了下面的对话“是我病了以后，我的妻子赶到来照顾我。”“所以说您来英国的时候，您的妻子不在英国，还是在英国？”“我病了以后，她才和我见面的。”“所以并不像报道所说她已经在英国了，你才到这来的？”“不是这样。”<br />
<br />
记者其后向新华社伦敦分社负责人确认万武义的夫人到英国的日期。这位负责人说，万武义的夫人是5月中旬跟一个旅游团到英国的，比万武义早到。旅游期间万武义病倒，于是赶来照顾他。<br />
<br />
采访期间，万武义一再强调，网上传言“毫无根据”并且他也“不在意这些东西”。他对记者说，“我是一个农民的子弟，从小学，中学一直到大学都是靠救济金，助学金的帮助完成学业。工作后能够迈入新华社的大门感到非常荣幸。我热爱我的事业就像热爱我的眼珠一样，不存在其他问题”。<br />
<br />
采访期间，新华社伦敦分社的负责人一直在场，并向我们介绍见到了万武义的夫人。<br />
