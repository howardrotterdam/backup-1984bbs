&nbsp; &nbsp; <br />
传中宣部组长、新华社主任万武义出走，未证实<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;明镜网特约记者 吴章<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 北京新华社总社一位退休的资深人员对明镜网说，传中宣部新闻协调小组组长、新华社国内部主任万武义日前擅自出走英国，与先期已在英国的妻子团聚。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 但是这条传闻未获官方证实。新华社总社一位官员说，新华社也不能确认此事。现在不知万武义是暂时离队，还是真的出走了。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 知情人士对明镜网(mirrorbooks.com)，“万武义和中组部一个团访问英国。他说腰疼病复发，不能走路，代表团就把他留下，改住一个价格低的小旅馆。”<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 如果这个消息证实，万武义就是近二十年来新华社出逃的最高级别官员。1990年代出走的新华社香港分社长许家屯和副社长郑华，并不真正属于新华社系统。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 现在不知万武义出走的动机，万武义是一个得到中共高层器重的新华社记者、宣传官员。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 中共陕西省委曾于1985年对他予以通报表扬，省委宣传部发出了《关于学习万武义同志深入实际调查研究的决定》；1985年6月新华社总社发出《嘉奖令》，对万武义通令嘉奖，号召全社编辑记者学习他深入基层调查研究的采访作风。1998年，在新华社湖北分社社长任上被评为湖北省有突出贡献的中青年专家。 1999年起享受国务院特殊津贴。2000年获得第四届全国百家新闻工作者称号。他同时也是中国第九届长江韬奋奖获得者。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 但是，新华社总社一位资深人员对明镜网形容万武义时说，“此人是工农兵学员，没学问，好争名利，擅长打击人，自私、贪污。”一直有人举报万武义。明镜月刊之前披露了新华社高层的腐败案件。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 今年元月，新华社副社长(副部长级)、中国报业协会副主席崔济哲被“双规”。明镜月刊说，崔济哲把持新华社人事局十来年，卖官受贿，涉及总社一批局处级官员以及一些分社主管。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 知情人说，崔济哲就涉嫌在外地接受企业老板的色情招待，与三陪女洗鸳鸯浴，便动用了新华社内参权力，为企业老板向中央“进言”送虚假资料<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 新华社资深人员对明镜网说，崔济哲腐败案件涉及万武义。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 另有消息说，万武义是因不满中共对新闻的控制而出走。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 万武义，1952年3月出生，中共党员，高级记者，正局级干部。1976年西北大学中文系毕业后被分配到陕西安康地委任通讯干事。1984年调入新华社陕西分社，历任记者、信息部主任，湖北分社副社长、社长，从2000年开始，调入新华社总社任国内部任常务副主任，2002年升任国内部主任。</div>

							
							
							
							
