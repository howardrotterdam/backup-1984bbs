													<h2>杭州老人街头晕厥 数十围观者无人施救 不治身亡</h2>
						
						
																											<div id="postmessage_362721" class="t_msgfont">　　浙江在线12月08日讯前天上午，家住杭州紫阳街道大狮子巷62岁的丁大伯，上街后再也没能回到家里。中午12点左右，在靠近中山路的积善坊巷，患有冠心病的丁大伯突然一头栽倒不省人事，经抢救无效不幸去世。<br />
<br />
　　当时，不少路人目击了丁大伯倒地，可是大家除了打120和110之外，不知也不懂该如何展开急救。<br />
<br />
　　等110和120赶到将老人家送到医院已经40分钟过去了。参与抢救的省中医院苏医生表示，这类突发的心血管方面的病症，争取抢救时间最为关键，病发后超过30分钟没采取急救措施，病人就会非常危险。<br />
<br />
　　面对倒地的老人，没人敢碰一下<br />
<br />
　　事发当时，周先生正在旁边公园里散步。<br />
<br />
　　“中午12点左右，我看他走过来的，手里提着个袋子，嘴里还含着饮料吸管，走着走着就一头栽倒了，头撞击地面的声音很大，大家都听到了。”<br />
<br />
　　丁大伯的头直接撞上了水泥地面，来不及伸手撑一下身体，头上一下子流出了血。<br />
<br />
　　公园里当时有五六个人。大家都围了过去，看看怎么回事。人越聚越多。<br />
<br />
　　“我记得有个人说老人摔倒了是不好随便去碰去抬的，我们不是专业的，万一碰到了哪里，加重伤情怎么办？”周先生说。<br />
<br />
　　围观的很多人也都这么觉得：不要贸然去动老人，说不定会越帮越麻烦。<br />
<br />
　　不过，很快就有人去叫社区工作人员来现场处理。<br />
<br />
　　第一个打120的就是附近东平巷社区的工作人员屠玉聪。<br />
<br />
　　“有人跑到办公室来告诉我有个大伯倒在路上了。”屠玉聪表示自己并不是第一时间出现在现场的。“当时现场已经聚集了20人左右。”<br />
<br />
　　另一位目击者郭大伯说，丁大伯头侧偏躺在地上一动不动，嘴里也没有发出声音。<br />
<br />
　　医生说错过了最佳的救治时间<br />
<br />
　　当天中午12点19分，接到报警电话的湖滨派出所乔警官放下饭碗和同事直奔现场。<br />
<br />
　　积善坊巷是一条小巷，不住这里或是对杭州不太熟悉的人，要找到它不那么容易的。因为巡逻车经常经过这里，乔警官和同事倒很快到了现场。<br />
<br />
　　怕120找不到地方耽误时间，乔警官又给120打了电话，告诉他们如何以最快的速度赶到积善坊巷。120急救车赶到后，马上把老人送进了省中医院，在路上也展开了急救。<br />
<br />
　　可还是太迟了。<br />
<br />
　　老人送到医院的时候已经是中午12点40分左右了。送到时，丁大伯瞳孔已经变大，呼吸和心跳也已经停止了。在省中医院参与抢救的苏医生说，老人患有冠心病，心血管也存在病症，加之长期抽烟喝酒，导致病症突发。“这样的情况，如果在半个小时之内救治，或是采取了最快的急救措施，还有希望救命。”<br />
<br />
　　“有这种病的老人，不该让他落单啊，最好有人陪着。”苏医生很是惋惜。<br />
<br />
　　在联系丁大伯家人的时候，民警发现大伯居住的房子就他一个户口。“他是一个人住的，子女都在外地，就他的弟弟在杭州。”<br />
<br />
　　邻居说，丁大伯喜欢去大华书场听书，那个时候他可能正好听完书从书场回来。<br />
<br />
　　太理智还是太冷漠<br />
<br />
　　闹市街头，老人倒地不省人事，几十人围成一圈，弯腰看着老人，除了打电话报110和120，人人束手无策。<br />
<br />
　　民警觉得十分惋惜。“要是能多一点抢救时间就好了，可惜太迟了。”<br />
<br />
　　如果这几十人当中有一个人就地拦车把老人送到医院，又或者能够找找老人身上有没有家人的留条或者急救药，丁大伯也许还能去大华书场听书。<br />
<br />
　　采访中，有目击者说“年纪大的人摔倒在地，是不能随意搬动的，尤其是在不了解老人倒地原因的情况下，否则会越帮越麻烦的”，所以他们用他们认为最正确最不会惹麻烦的办法拨打了110。<br />
<br />
　　也许我们不能说围观者冷漠，可是一念之间，丁大伯的命就这样没了，是事实。<br />
<br />
　　医生说，如果发现老人摔倒在地，应该看清楚他的状况，可以从老人的衣袋里轻轻翻找一下，看有没有家人留言，随身带的急救药等等，或者查看有没有家人电话，以进一步了解病情，以便施救。<br />
<br />
　　如果老人没有了呼吸，现场人员更应该立即对他进行心肺复苏急救，比如口对口呼吸，挤压胸部等等，这是常识。<br />
<br />
　　倒是老人神志清醒有呼吸的前提下，可以不随便去动他，拨打110、120，让专业救护人员前来抢救比较科学。<br />
<br />
　　采访回来，心里很不是滋味。我们到底怎么了，是太理智还是太冷漠了？生命在消逝，我们却怕惹麻烦上身而不敢出手相救；有人捡到钱包物归原主，因为钱包里的钱少了，非但没听到谢谢反而还招来失主怀疑的眼神……<br />
<br />
　　医生的这类提醒，我们已经刊登很多遍了，医生说那些急救措施是常识，但愿下次面对街头倒地的老人，会有更多人愿意上去“多管闲事”。</div>

							
							
							
							
