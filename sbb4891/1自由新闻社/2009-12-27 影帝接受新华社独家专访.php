																											<div id="postmessage_399666" class="t_msgfont">温家宝回应网民反映房价涨得太快问题<br />
2009-12-27 16:04:45　来源: 中国新闻网(北京)　<br />
<br />
12月27日，中共中央政治局常委、国务院总理温家宝在北京中南海紫光阁接受新华社记者独家专访，就当前经济形势、明年经济工作和其他问题回答记者提问。新华社记者姚大伟摄<br />
<br />
中新网12月27日电 中共中央政治局常委、国务院总理温家宝今天下午在中南海接受新华社专访，对于网民反映房价涨得太快的问题做出回应。<br />
<br />
温家宝说，我知道网民关注这个问题。因为我每天上网看到大量网民的意见，甚至很尖锐的批评。因为房子对于每个人来讲都关乎他们的切身利益。<br />
<br />
温家宝说，今年房地产有了比较快的恢复，但同时房价在一些地区和城市上涨过快，也引起了中央高度重视。如何使房地产建设能走入一个健康的轨道，首先还需要弄清楚政府要做什么，市场要做什么。既发挥市场的机制，又发挥政府调控的作用。<br />
<br />
温家宝强调了政府应该做的事情。他说，第一件事情就是要加大安居工程的建设力度，加快棚户区的改造。在资金、土地以及税收上，都要给予优惠和保障。<br />
<br />
<br />
第二，要鼓励居民购买自住房和改善性的用房。但与此同时，要采取措施抑制投机。中国的国情决定了我们的住房必须坚持安全、经济、适用和省地的原则，特别要重视中小套型和中低价位房屋的建设。<br />
<br />
第三，要运用好税收、差别利率以及土地政策等经济杠杆加以调控，稳定房地产的价格。<br />
<br />
第四，要维护房地产市场秩序，打击捂盘惜售、占地不用、哄抬房价等违法犯罪的行为。只要政府有决心解决这个问题，而且不是头痛医头、脚痛医脚，而是经过深入调查研究，统筹考虑各方面的情况，制定长远的规划和政策，使我国的房地产有一个稳定发展的局面，这是可以做到的。<br />
<br />
