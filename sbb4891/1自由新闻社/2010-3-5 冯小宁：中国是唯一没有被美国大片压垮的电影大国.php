　　 美国人深谙这个道理，于是他们的电影几乎没有一个正面歌颂美国这个国家、美国政府和权力机关的，他们把一个又一个普通的美国民众拿出来神化，赋予他们美国精神和美国立国的核心价值观。于是，全世界的观众都从好莱坞电影中看到那些普通的却拥有先进思想、独立精神的美国人，即便他们反对的是虚构中的美国腐败政府，也让人家肃然起敬， <br />
　　 <br />
　　 国家和个人，就这样在美国人的好莱坞电影里得到简单的归纳，政府和权力机关不代表国家，而一个国家最好的体现反而是生活在其中的国民――他们的精神就是美国精神。美国人把这种思维注入文艺作品中。不信吗？当你看完一部好莱坞电影，一个普通人终于把整个警察局搞垮了之后，一个低级的工作人员把白宫打败的时候，你的第一感想是什么？啊，原来掌握了生杀大权的警察局也能够受到限制，也能够被民众打败（毕竟全世界的警察局都一样），原来美国人才代表正义，原来美国竟然能够出产这种英雄，真了不起啊！ <br />
　　 <br />
　　 <br />
　　 <br />
　　和美国站在另一个极端的是那个和我们一衣带水、世代友好的北朝鲜，据说最近为了提升国际地位，赢得国际社会的尊重，它拥有了核武器，还到处威胁随时使用它。这国家牛吧！原子弹只使用过一次，那就是当日本人在中国烧杀奸淫、妄图霸占整个亚洲、也想征服美国的时候，美国人使用了一次。现在人家北朝鲜也有了，随时准备使用。这北朝鲜国际地位不高？你还不敢不尊重人家？ <br />
　　 <br />
　　 <br />
　　 <br />
　　北朝鲜留在我们脑海里的另外一个场景是那个国家的广场，在这个国家的领导人生日那一天，十万整齐划一如木偶般的朝鲜人大聚会，十万人整齐地载歌载舞，高呼领袖万岁――这种十万人同一时间做出一丝一毫都没有差别的动作，请问，这个世界上，还有哪一个国家能够做到这一点？你不佩服这个国家？ <br />
　　 <br />
　　 <br />
　　 <br />
　　有什么样的国家，就有什么样的国民；有什么样的政府，就有什么样的民众。像北朝鲜那种侵犯人权，剥夺民众言论自由，迫使人民冒死偷渡到中国的国家，就算它拥有了世界上所有的核子武器，你会尊重这个国家吗？ <br />
　　 <br />
　　 <br />
　　 <br />
　　美国熟能生巧地掌握了这一道理，在全世界推销它自己，提高它的国际地位。与它独一无二的军事实力相比，它的这种巧和软的力量显得更加有效。例如在中国大陆，普通人无法感觉到美国军事力量的威胁（中美用武力对决可能不会到来），可是你看看文化市场，现在的年轻人，有几个不是喜欢好莱坞电影超过国产电影的？你要去投资忽悠外国人，你不怕外国人没有被忽悠住，中国年轻的一代早就被人家忽悠过去了？ <br />
　　 <br />
　　 常常听到人家说，一些人不懂中国的民族性，不懂得中西差别，外国人讲究个人主义，以个人为中心，中国人重视集体主义，以国家为中心。果真如此吗？你说的是什么时候的事？我想，不懂的不是我，而是你。你一定既不懂外国，也不懂中国。你平心静气地到世界各地转一圈，看看这个世界上还有几个民族的国民比中国人更以个人为中心，更加自私自利，置群体利益于不顾？你再回想一下，一个标榜以集体和国家为中心的国家怎么会在日本人侵略中国时出现了世界历史上从来没有过的巨量的汉奸？ <br />
　　 <br />
　　 <br />
　　 <br />
　　 那些说中国人以家族和集体利益为中心的，只不过是要以他们个人为中心而已，那些强调所谓中国人忘记个人，而倾向于以国家利益为中心的，几乎从来都是劫持了国家的利益集团在那里自说自话，实际上，走了那么多地方，我现在“可以负责任的说”：最没有集体观念，最一盘散沙，最以自己为中心的就是我们这个被破坏了核心价值观的民族…… <br />
　　 <br />
　　 <br />
　　 <br />
　　 如果我们自说自话，关起门来搞宣传，没有什么问题，但如果要到海外去花钱设立媒体包括电视，不仅仅让外国人看，而且想让他们通过我们的节目认识中国，对中国刮目相看，唯一的办法，不是对“国家”的假大空的宣传，更不是对政府和权力机关竭尽肉麻的歌功颂德，而是要从生活在这个国家的人民入手…… <br />
　　 <br />
　　 <br />
　　 <br />
　　这一切绕来绕去看上去很复杂，其实很简单，归根到底，就是在推广中国、宣传中国的时候，把目光集中在中国人身上，真正做到“以人为本”――只有中国人活得有尊严、有地位，我们的国家才能赢得世人的尊重，中国的地位也才会真正得到提高！<br />
