　　在和平年代，维持一个国家的正常人口基数，其生育率应不少于2&#8226;1，即平均每对夫妇要生2&#8226;1个孩子。而现在的欧洲，平均生育率是1&#8226;38，其中俄罗斯为1&#8226;14，西班牙为1&#8226;1。这样发展下去情况将如何呢？<br />
