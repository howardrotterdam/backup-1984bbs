核心提示：当地时间13日，法国下议院对罩袍禁令进行投票。结果显示，下议院以355票赞成1票反对的压倒性票数通过该禁令，德国，英国，和西班牙的绝大多数民众也支持罩袍禁令。<br />
<br />
中新网7月14日报道 据外媒报道，当地时间13日，法国下议院对罩袍禁令进行投票。结果显示，下议院以355票赞成1票反对的压倒性票数通过该禁令。<br />
<br />
根据该禁令，在任何公共场合穿戴穆斯林罩袍皆为非法行为。此外，禁令还拟定对穿戴面纱的妇女罚款150欧元，而强迫妇女穿戴面纱或罩袍的男子将面临一年有期徒刑或罚款1.5万欧元。<br />
<br />
罩袍禁令得到法国民众的支持。美国华盛顿智库的一项调查显示，82%的法国民众支持禁令，而持反对意见的仅有17%。调查结果还显示，德国，英国，和西班牙的绝大多数民众也支持罩袍禁令。<br />
<br />
罩袍禁令在被立法前还需送至参议院进行表决，预计投票将于9月份进行。<br />
<br />
法国行政法院则警告说，此禁令可能违背宪法，另外也会遭到国际人权组织的反对。<br />
