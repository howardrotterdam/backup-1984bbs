　　于是，1929年复活节，伯奈斯策划了一场大游行：刚刚成年的少女们，手持作为“自由的火炬”的香烟，在纽约第五大道招摇过市。从此，女性烟民逐渐增多，多到今天成为一个严重的健康问题了。<br />
<br />
　　这场被视为女权运动史上重大事件的游行，尽管缘于一场商业策划，但女人们对香烟的真实感受并不能被否认。也就是说，在舆论动员中，她们真找到了解放的感觉。你也可以说，是她们心中既有的渴望，被伯奈斯和烟草公司利用了。 <br />
<br />
　　同样道理，无论“贾君鹏你妈妈喊你回家吃饭”来自何处，一句真实的留言也罢，一个无聊的涂鸦也好，或者真是黄亮华团队的策划和推动，那些没有参与公关活动的网民心中的确因此而产生了种种情感反应――寂寞、感动、无聊、起哄、联想等等。我仍然认为，这是一篇最佳微型小说，采撷的是人们再也熟悉不过的一个日常生活片断，提供了充沛的想象空间，预示了丰富的阅读体验。<br />
<br />
　　我最感兴趣的是，人们利用这句话进行了再创造，无论是歌曲还是宣传海报，都远远超出了一个无聊留言或者一个商业策划的价值。<br />
<br />
　　有网民把这句话当作政治动员、标语口号，贴在大街上、游行队伍的横幅上、烈士塑像的底座上、春运时的火车站里、农村的计划生育标语墙上、标志性政治建筑的铭牌上，或者让它从政治人物、新闻发言人、电视主持人的口中说出来，或者做成领袖题词形式的书法作品，构成一种政治波普艺术。看见这些东西，我觉得非常有意义。不要简单地说它们无聊。政治波普艺术大师安迪・沃霍尔说过：“我喜欢无聊的东西，我喜欢一样的、可以被不断地重复的事。” <br />
<br />
　　但在另一方面，这并不意味着，商业策划活动中的欺骗和舆论操纵就能够得到人们的认同。可以肯定地说，如果一开始就知道这是一个公关策划，而不是一个母亲的真实呼唤，或者一个网友的无聊涂鸦、一个游戏玩家的寂寞留言，人们的心理反应会有很大不同。公关活动者一直都在玩危险的游戏，行走在社会舆论和商业伦理的敏感边缘。<br />
<br />
　　那些悲观的论者认为，包括网络在内的新闻媒体早就被政治和商业公关所包围，它们从来都不是在报道新闻或讨论问题，而是在报道公关公司告诉他们的事情，讨论如何操纵消费者或选民。我对舆论并没有这么绝望，但是这些看法值得我们随时警醒。 <br />
<br />
