													<h2>贾君鹏你妈妈喊你回家吃饭！</h2>
						
						
																											<div id="postmessage_168879" class="t_msgfont"><img src="http://i32.tinypic.com/15qsmye.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
<a href="http://bbs.city.tianya.cn/tianyacity/Content/5026/1/5274.shtml" target="_blank">http://bbs.city.tianya.cn/tianyacity/Content/5026/1/5274.shtml</a><br />
2009-7-19 8:38:47<br />
南方报业网/散人 以往的网络上，网民暴得大名多少是有些原因的，多少是现实世界中可以找到逻辑解释的行为，诸如最早的竹影青瞳、芙蓉姐姐，以后的天仙妹妹或者任何做出出格事情而触动了社会神经的人。无原因成名，目前大概只有被“喊回家吃饭”的贾君鹏了。<br />
<br />
据《新京报》报道，那是在7月16日10点59分在百度“魔兽贴吧”中一个名为《贾君鹏你妈妈喊你回家吃饭》的空帖，以接近爆炸的方式迅速蹿红。短短两天时间，百度贴吧上的跟帖已经超过24万，目前每一秒钟就有数千跟帖刷新这一数字。<br />
<br />
各路专家并不能解读这样的一个事件，因为这根本就说不通嘛，无论是传播学还是社会学，这都是说不通的一件无厘头事件。《新京报》跟踪了此事，给出的答案是两个字:寂寞。魔兽世界是著名的网络游戏，现在更换了服务商，但除了这个因素之外，迟迟拿不下来的审批也让这个游戏的无数玩家很寂寞。个人觉得这个说法颇为靠谱，但只能说明一个事件的起因，对背后的原因与以后意义的延展，并不具备说明意义。<br />
<br />
大致上“被喊回家吃饭”的重点在那个“喊”字上，可以把这个看做是一种呼唤，在这里是呼唤一个为人子者回家吃饭，引申的意思就是呼喊一些东西赶紧回来。在魔兽世界的玩家看来，这是隐隐约约的呼唤游戏赶紧回来，与“打酱油”一样，是一种隐晦表达的意思。<br />
<br />
网络世界的语义进化速度超过了现实世界，很快就如同“打酱油”一词的语义转换一样，成为了一种表达自己心情的方式，从而产生了某种心照不宣的流行。大家虽然对这个各有解读，但生活里总有一些东西是我们想要回来而又要不回来的，我们对此又无能为力，正如魔兽世界中那些玩家盼不回的游戏以及账号上的钱一样，我们都知道这个事情实在是荒诞可笑，只是大家没有任何办法。<br />
<br />
再则，大家也没有更好的语言来描绘自己的心情，完整地写个帖子吧，那个门槛还是挺高的，而且有用没用另说，写过分了还会被删除。所以，在我们这里的网络世界中，就充斥着无数奇怪的意向式表达，从打酱油到围观、从虚构的神兽草泥马到看上去温情的“妈妈喊你回家吃饭”，一个所指模糊但又清晰的东西，成为了一种无法表达中的表达形式。<br />
<br />
不知道是否有人会因此而想起《黑客帝国》这个电影？在同步发行的动画形式的DVD中讲了与“母体”有关的10个故事，好几个故事都讲到了在“母体”中生活的人是如何觉得不对劲，但是不知道应该如何表达的意思，他们总是通过某些特殊的机缘发现了真相，可是这个真相又无可言说。不能言说的痛苦是惊人的。<br />
<br />
更多的人没有机会发现真相，但也知道有些事情不对，比之无可言说来，语不成话没有那么痛苦，只是迷茫更甚罢了，以至于迷茫到需要“妈妈喊你回家吃饭”作为口号的程度。<br />
<br />
<img src="http://img.blog.cctv.com/attachments/2009/07/495576_200907192004181.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
我们先看看“贾君鹏”蹿红网络的几个标志性事件：<br />
　　<br />
　　<br />
　　 7月16日，上午10：59分，某IP地址为222.94.255.*的网友匿名发帖《贾君鹏你妈妈喊你回家吃饭》，在短短五六个小时被390617名网友浏览，并已有17028名网友跟帖回复。<br />
　　<br />
　　<br />
　　 7月17日，百度贴吧删帖后又恢复了帖子，并在首页以《回家吃饭引爆玩家寂寞》进行置顶，并配发了“星爷”的PS恶搞图片，并在这个标题下面推荐了其他相关引导性帖子。<br />
　　<br />
　　<br />
　　 7月17日，《信息时报》以《半天走红网络，贾君鹏是谁？》发表文章，迅速吸引各大网站转载。<br />
　　<br />
　　<br />
　　 7月18日，天涯社区首页以《贾君鹏，你妈叫你回去吃饭》为题，推荐帖子至天涯非常有影响力的栏目“热点推荐”下面，帖子内容是针对“贾君鹏，你妈叫你回去吃饭”为主题的网友PS恶搞大集合。<br />
　　<br />
　　<br />
　　 7月18日，《新京报》、《楚天都市报》分别以《“贾君鹏”蹿红网络》和《百万网民一天捧红“贾君鹏”――“你妈妈喊你回家吃饭”成网络流行语》为题对这一网络现象进行了跟踪报道，并在更大范围内被各大门户、新闻网站转载。<br />
　　<br />
　　<br />
　　 同样，在7月18日，《河南商报》一篇评论文章《神奇“贾君鹏”书写的网络寓言》发布，质疑贾君鹏网络蹿红和网民狂欢的声音出现。<br />
　　………………………………………….<br />
　　<br />
　　 截止笔者写作时间，《贾君鹏你妈妈喊你回家吃饭》在百度贴吧的点击率已经突破80万，回帖量已经突破30万，不断的刷屏，不断地有新的回复新的点击，正如新京报报道所言：任何人的跟帖会迅速被淹没，在贴吧里，即使你点开跟帖“尾页”，结果发现后面已经又有数页跟帖，以至有人说“尾页只是个传说”。<br />
　　 贾君鹏的网络蹿红，迅速成了一个网民狂欢事件，一夜间，网民给贾君鹏搭建起了完整的家谱，爸爸、妈妈、爷爷、奶奶、老婆、女友、第三者等等一应俱全，至于贾君鹏是谁已经无关紧要，是否确有此人也不是网民关心的话题。大家只知道，此时的贾君鹏是一个网络狂欢的符号，是一个发泄寂寞的窗口。<br />
　　 这一奇特现象也反映出了网络特有的文化。恶搞、盖楼、抢楼、PS、人肉搜索等诸多网络流行文化在这一事件中全部都反映了出来，俨然一场网络文化的狂欢。<br />
　　 当然，对于贾君鹏的网络蹿红，不管是媒体还是网民，至今还是一个谜。有人推测是“贾君鹏你妈妈喊你回家吃饭”这句熟悉的话语激起了儿时亲切的回忆，引发大家的共鸣；有人推测贾俊鹏的迅速走红正是千千万万个“贾君鹏”因为痴迷游戏而对家庭有所歉疚的表露；有人推测是网络的恶搞文化和网民的寂寞心态成就了这一网络神帖。当然，针对“贾君鹏你妈妈喊你回家吃饭”为什么可以网络蹿红的原因众说纷纭。<br />
　　 至于其幕后隐隐出现“魔兽世界”和“网络推手”的身影，将继续带领大家一窥全貌。<br />
