																											<div id="postmessage_188417" class="t_msgfont">“贾君鹏”已从网络世界走向现实，无数的人都在借此恶搞或表达情绪。<br />
<br />
&nbsp; &nbsp;你知道近来风靡万千网民、专家趋之若鹜的“贾君鹏”吗？谁都不知道他是什么人，他干过什么事，但仅凭有人在网络上发了个12字帖子“贾君鹏你妈妈喊你回家吃饭”，他就成了中文网络世界的奇迹。<br />
<br />
&nbsp; &nbsp; 这12个字的感召力在于，在短短几天内，它获得了几十万条的回复，带来几百万的点击量，它被众多的网友口口相传，成为时下最火的流行语。他不仅引来网民观众，甚至引起各领域专家的关注、评论，以及境外媒体的聚焦。有人如此给它下定义：一句呼喊，喊出了千千万万寂寞的灵魂。<br />
<br />
&nbsp; &nbsp; 就在“贾君鹏”事件开始被赋予各种深层次涵义、各路人马开始怀疑这是一次幕后推手策划的炒作事件之时，北京一传媒公司突然自曝，是他们制造了“贾君鹏”，目的是帮助一款游戏保持关注度和人气。该策划“总计动用网络营销从业人员800余人，注册ID20000余，回复10万余”。这个创意也让他们赚了“6位数”。<br />
<br />
&nbsp; &nbsp; “贾君鹏”的的确确把大众娱乐了一把。<br />
<br />
&nbsp; &nbsp; 网络新宠<br />
<br />
&nbsp; &nbsp; 7月16日上午10时59分，网络一贴吧里，IP地址为“222.94.255.*”的网友发了一帖，帖子标题为“贾君鹏你妈妈喊你回家吃饭”，内容只有“rt”两个字母，意思为“如题”。就是这么一句近乎调侃的话，以星火燎原之势迅速“烧”红网络！<br />
<br />
&nbsp; &nbsp; “贾君鹏”是一粒小得不能再小的石子，奇怪的是，它竟然激起了很大的涟漪。这篇只有标题的空帖在发出短短6小时内就获得了39万多的点击率，超过17000名网友参与回帖。截至20日18时，回帖数已经达到31万多，点击率已达1084万多，而这仅用了4天时间。<br />
<br />
&nbsp; &nbsp; 一个原本名不见经传的“贾君鹏”突然火起来，这被不少网友称为“中文网络世界的奇迹”。<br />
<br />
&nbsp; &nbsp; 推手曝光<br />
<br />
&nbsp; &nbsp; 真有“贾君鹏”其人，还是有幕后推手制造了此次网络事件？答案是后者。黄亮华，北京一家传媒公司的CEO，网络营销专家，服务于各大知名品牌。在博客中自我曝光是“贾君鹏之父”。<br />
<br />
&nbsp; &nbsp; “其实最早我们做了两个方案，比如没人理这个帖子怎么办？火爆了怎么样？只不过现在这么热，有点始料未及。”黄亮华今年27岁，他的公司有10名员工，每年这样的“炒作”业务，大概有3到5个，“平时常规的营销比较多。炒作有特定的客户要求”。<br />
<br />
&nbsp; &nbsp; 黄亮华透露，“贾君鹏”事件，他们进行了两个月的反复思量和流程设计，动用了四个执行席媒介，轮班监测执行情况，两小时一次电话汇报。“总计动用网络营销从业人员800余人，注册ID 20000余，回复10万余。”而大量的网络任务是交给兼职的网民去完成的。在引发轰动效应后，策划团队撤出，他们的回帖只占真正网络回帖的3%。<br />
<br />
&nbsp; &nbsp; 赚到6位数<br />
<br />
