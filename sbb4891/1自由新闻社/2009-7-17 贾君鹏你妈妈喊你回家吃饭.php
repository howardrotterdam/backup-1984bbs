													<h2>解放日报：贾君鹏的寂寞是网瘾作怪</h2>
						
						
																											<div id="postmessage_181360" class="t_msgfont">复旦大学心理学系主任、教授、博士生导师 孙时进<br />
问：7月16日，百度“魔兽世界”贴吧出现了这样一个帖子“贾君鹏你妈妈喊你回家吃饭”，这篇只有标题没有内容的帖子在3天时间里引来31万回帖。这句生活语言，道出了沉迷游戏中少年被呼喊的人生常态。请问，谁在呼唤贾君鹏回家吃饭？“呼唤贾君鹏”是网瘾作怪吗？<br />
―――上海河南中路 网民小峰<br />
答：为何这样平白简单的一句话会激起如此巨大的反响，各方人士对此作出了不同的解释。<br />
<br />
　　一种观点认为，“贾君鹏你妈妈喊你回家吃饭”意指“魔兽世界快点交给网易，快点重新开张”，是网友呼唤魔兽世界快点回来。但总的来看，大部分人觉得这种说法并不靠谱。事实上，“魔兽世界”吧虽然是一个游戏主题的贴吧，但贴吧中的内容大部分与魔兽世界游戏无关。参与者大部分也并不一定熟悉和了解魔兽世界。<br />
<br />
　　另一种分析认为，“呼唤贾君鹏”是呼唤魔兽世界的代名词，其本质上反映了部分网友依赖网络、依赖网游的网瘾存在。贾君鹏可能是一个沉迷网络的小孩，他的朋友知道能在这里找到他，所以在这里呼唤他回家。“贾君鹏你妈妈喊你回家吃饭”这句典型的生活形态语言，是大家和焦急的妈妈一起在呼唤沉迷于网络游戏中的孩子回归现实和社会。<br />
<br />
　　当今，网络成瘾尤其是青少年网络成瘾，确实已成为困扰很多家庭和学校的一个社会问题。一般而言，网络成瘾可以划为如下5种不同类型：1、网络游戏成瘾：指强迫性地沉溺于电脑游戏或编写游戏程序；2、网络关系成瘾：指沉溺于通过网络聊天工具、交友网络等来结识朋友。例如，沉溺于QQ、MSN等聊天工具；3、网络信息成瘾：指强迫性地浏览网页查找和收集大量无用的信息。例如，无法控制地通过网络搜索引擎收集大量无效的信息；4、网络交易成瘾：指源于一种难以抵抗的交易冲动，着迷于网上贸易或者拍卖、购物、在线赌博等而不能自拔，过分地使用网上购物场所；5、网络色情成瘾：指沉迷于成人话题聊天室和色情网站，或沉迷于网上虚拟性爱等，导致精神萎靡，不能进行正常的工作和生活。研究发现，在所有的成瘾类型中，“网络游戏成瘾” 和“网络关系成瘾”者占绝大多数，高达80%以上，是最具代表性的网瘾群体。因此，在这个以著名网络游戏为名的贴吧出现的这条帖子，能够引起人们关于网络成瘾的不断联想，也就不足为奇了。<br />
<br />
　　网络成瘾是一个心理医学概念，其核心是由于某些特定的心理诱因，对于互联网的依赖超过正常限度。美国心理学家杨格曾提出，诊断网络成瘾有10条比较简单的评判标准：上网时全神贯注，下网后念念不忘“网事”；总嫌上网时间太少而不满足；无法控制自己的上网行为；一旦减少上网时间就会烦躁不安；一上网就能消除种种不愉快情绪，精神亢奋；为了上网而荒废学业和事业；因上网放弃重要的人际交往、工作等；不惜支付巨额上网费用；对亲友掩盖自己频频上网的行为；有孤寂失落感。从上述标准中可以看出，网络成瘾对于现代社会人们工作、生活产生的巨大负面影响。<br />
<br />
　　尽管“贾君鹏事件”与网络成瘾的关系或许并不是最密切，但互联网已经深深地根植于现代人的生活、工作、娱乐，并改变了现代人的行为方式、交流方式、娱乐方式等，进而影响人们的思维方式，这已经无需再论证。在互联网“双刃剑”特征凸显的今天，准确界定互联网与现实世界的距离，既充分运用网络技术为人类服务，又避免网民过度依赖互联网弊端的出现，形成健康理性的网络声音，确实需要认真考虑。<br />
