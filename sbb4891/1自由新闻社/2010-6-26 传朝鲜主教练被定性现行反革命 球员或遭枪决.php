　　美国“自由亚洲电台”（RFA）昨日报道称，由于朝鲜与巴西进行的首轮比赛时表现优异，根据朝鲜最高领导人金正日特别指示，朝鲜国内历史上第一次现场直播了葡萄牙队与朝鲜队的比赛。<br />
<br />
　　然而，朝鲜队以0比7的成绩惨败，让朝鲜上下极为尴尬。<br />
<br />
　　报道分析指出，朝鲜当局认为，通过此次世界杯上朝鲜队的表现，能提高民众对朝鲜体制的信心，但没想到这项措施反而产生了副作用。<br />
<br />
<br />
惨败给葡萄牙后，朝鲜后卫志尹南（左）和中场球员安英学（右）神情茫然。（路透社）<br />
　　报道称，朝鲜队曾在1966年的伦敦世界杯上击败意大利进入八强；但因为在八强复赛输给葡萄牙，朝鲜当局给所有球员戴上“反革命分子”的帽子，全部被送到煤矿工地进行劳动改造。从这个历史经验来看，此次参赛的朝鲜球员，被处罚的可能性也很大。<br />
<br />
　　从朝鲜逃往韩国的张海成透露：“当时参加世界杯的守门员朴承进是我大学同学。回国后，他和所有选手们都被送到位于咸镜北道生气岭的一家煤矿。”<br />
<br />
　　不过一些人认为，朝鲜政府可能会对国家足球队采取处罚措施，但不会像44年前那么残酷。<br />
<br />
　　10年前逃离朝鲜的一名人士说：“朝鲜政府当时的处罚措施，对朝鲜足球造成很大的打击。随着时代变化，处罚方式也会变化。这一次可能对选手们的思想进行批判来作为处罚吧。”<br />
<br />
　　朝鲜队教练金正勋日前接受媒体访问时说：“如果朝鲜国家足球队在本届世界杯上成绩不佳，选手们不会受到任何处罚。”<br />
<br />
　　另据体育频道ESPN报道，金正勋日前表示，朝鲜最高领袖金正日在每场比赛前都会通过“隐形手机”给予战术指导。金正勋表示，金正日亲自开发了这种“隐形手机”。<br />
<br />
　　朝鲜广播电台此前曾将朝鲜队在首战中的神勇表现，归功于金正日的三儿子金正银的“关怀”。他援引多名朝鲜高层消息人士的话说，4月和5月，金正银曾亲临训练场鼓励球员，并指示“要打造能攻能防的无敌之旅”，并让朝鲜球员“火线入（劳动）党”。<br />
<br />
