<br />
&nbsp; &nbsp;&nbsp;&nbsp;“粉粉砖”惊现绵阳灾区<br />
<br />
 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;本报记者&nbsp;&nbsp;刘建锋 <br />
<br />
 <br />
<br />
　　“制砖行业不光乱，而且由乱演化出了严重的质量问题，碳酸钙吸水膨化使页岩砖一点点变成砖粉，再来一个不大的地震，都很有可能又造成严重伤亡！”9月26日下午，四川安县晓坝中心砖厂老板吴佳明在北川县安昌镇说。<br />
<br />
 <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;“粉粉砖现象到现在还没引起重视。”10月底，吴佳明等对中国经济时报记者说。<br />
<br />
 <br />
<br />
　　“放两三个月，好好的砖就成了粉粉” <br />
 <br />
<br />
　　2009年9月，中国经济时报记者在一些砖厂发现，吴佳明所说的质量问题确实存在。<br />
<br />
 <br />
<br />
　　9月26日下午，安县桑枣镇，志国砖厂老板颜雪良从厂里堆积的红砖中，随意抽出一块，两手轻轻一掰，红砖便断成了两节。<br />
<br />
 <br />
<br />
　　甚至只用手指就能一点点把整块红砖削成泥灰，“跟玩魔术一样，只要放两三个月，好好的砖就成了粉粉。”颜雪良说。<br />
<br />
 <br />
<br />
　　安县晓坝中心砖厂厂主吴佳明、安县黄土镇喇叭砖厂的老板也做了一样的表演。<br />
<br />
 <br />
<br />
　　北川盛昌页岩砖厂、光明页岩砖厂、腾达页岩砖厂、建园建材、高茂建材、安县黄土镇的八一砖厂、三鑫页岩砖厂、菩提页岩砖厂等许多砖厂的老板，或者对着记者的照相机展示“手指碎砖”，或者对记者证实自己厂生产的标砖存在严重的质量问题。<br />
<br />
 <br />
<br />
　　发现问题时每厂已经烧出百万砖，而且许多都已卖出，有些粉粉砖被农民发现后，要求退赔，“有个地方我一块砖赔了一元钱。” 喇叭砖厂老板杨庆寿说。“最高的我赔过1元2角一匹。”吴佳明说。<br />
<br />
<br />
<img src="http://1832.img.pp.sohu.com.cn/images/blog/2009/11/6/16/19/12577ee8180g213.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
为此，三鑫砖厂在2009年1月到4月间亏损150万元，产量大的喇叭砖厂3月底投产，到4月也亏损了150万元。<br />
<br />
 <br />
<br />
　　“生坯非常好，使劲掰都掰不开，刚出窑时质量、外观都非常好，质监机构都发了合格证，可是堆放两个月后，砖块膨化松碎。”颜雪良无奈地说。 <br />
<br />
 <br />
<br />
　　制砖用页岩矿含钙量超标，多家砖厂质疑国土局 <br />
 <br />
<br />
　　有关方面认为是生产工艺问题，但连续几个月调整设备和工艺后依然如此，而且安县北川的新建砖厂普遍如此，于是他们便怀疑起用来烧砖的页岩矿产资源，安县国土部门现场取样之后拿走分析，结果让老板们大吃一惊――“含钙量有的高达26%，还有的高达34%。”喇叭砖厂老板杨庆寿说，“用这种矿产资源烧结的砖块，在空气中放置后便会吸水，碳酸钙与水结合膨化，逐渐使整块砖化成粉末。”<br />
<br />
 <br />
<br />
　　杨庆寿说：“矿产表土资源很好，见质量好，第二、三批便大量生产，哪知下面的矿产资源有问题。”<br />
<br />
 <br />
<br />
　　吴佳明对中国经济时报记者展示了厂里的整套设备，电脑控制、流水线生产、隧道窑，“这在中国是最好的砖厂设备了。”吴佳明说，“可居然生产出了最差的砖，放上三个月，钙点就会到处冒出来，跟魔鬼一样。”<br />
<br />
 <br />
<br />
　　矿产资源是从国土资源部门公开的招标拍卖会上拍来的，而国土部门为每家拍得者提供了《资源及其开发利用与环境影响评估报告》，中国经济时报记者拿到了多份由四川省地矿局川西北地质队编写的报告。<br />
<br />
 <br />
<br />
　　喇叭砖厂从国土部门拿到的报告称，矿石的氧化钙含量为0.58%到2.54%，而该砖厂向中国经济时报记者提供的西南科技大学分析测试中心完成的报告显示：氧化钙含量是19.1%。国土局报告中含钙量同样很低的黄土镇八一砖厂，四川省地勘局绵阳地矿检测中心的检测报告却显示氧化钙含量高达26.02%。<br />
<br />
 <br />
<br />
　　安县负责处理砖厂事件的发改局副局长钟守平对中国经济时报记者证实，安县拍卖的矿产资源中，有一些确实是有问题，含钙量高，国土部门提出可以走法律程序，但砖厂老板认为走法律程序很漫长。<br />
<br />
