　　事情是不是这样？安县县委宣传部新闻中心主任周保全９月２７日上午说，当时，市委要求抓紧新建砖厂，就算外来投资者不投标，县里也会自行组织开建新砖厂。而且２００８年１２月１９日，绵阳市召开新闻发布会公开称当时的政策是：“扩大产能的同时，降低门槛、放开办砖厂。”<br />
<br />
 <br />
<br />
　　腾达页岩砖厂老板吴德古说，２００８年９月找乡干部，一个乡干部就敢让自己边建边办手续，“把土地出钱整下来就能建厂，１０月，召集砖厂老板们开会，保证提供一切便利。”<br />
<br />
 <br />
<br />
　　由此，绵阳市国土局的规划被大大突破（该局报告称，规划新建砖厂１５１家）。<br />
<br />
 <br />
<br />
　　周保全叙述了安县的委屈：“安县的重建农户本有９．４８万户，按每户平均用砖４万匹计算，便需３６亿多匹砖，目前安县也就是５０家厂，年总产１０亿匹左右，如按部就班，三年重建，甚至两年完成农房重建，这些新上的产能都可消化。但当时要求抓紧开建，安县受到的压力非常大，外地运来了大量高价砖，把市场快速抵消掉了。”<br />
<br />
 <br />
<br />
　　周保全认为“砖厂上访是不应该的”，“他们当初要求开办砖厂的时候，都称来支持灾区重建，政府怎么能不批呢？”<br />
<br />
 <br />
<br />
　　安县发改局副局长钟守平也说：“当时安县老厂３０家，技改和扩建后年产能只有３亿匹左右，市委决定各重灾区新办一批砖厂。当时找县领导说情送礼要办砖厂的多得很，领导躲起不见，最后以公开拍卖矿产资源的方式来解决问题，按本县需求总量和新北川建设因素，产能其实并不过剩。但上级要求加快农房重建，还曾要求在今年五月基本完成，安县的压力很大，市、县、乡镇一级一级向下加压……所以，新砖厂投产的时候，安县乃至整个绵阳市的农房建设都已进入尾声……”<br />
<br />
 <br />
<br />
　　绵阳市委宣传部给本报的邮件中仍说：“加快灾后恢复重建，尽快住上永久性住房，也是广大灾区人民的共同愿望，特别是在受灾最重的极重灾区北川、安县等地，广大受灾群众加快住房重建、早日实现安居目标的愿望十分强烈。另外，加快农房重建，也是去年我市确保受灾群众安全越冬，确保实现‘不冻死一人’目标的重要保障措施。因此，我市采取一系列措施加快灾后恢复重建步伐，既符合党中央、国务院和省委、省政府的决策部署，也满足了灾区加快重建的实际需要，既顺应了灾区广大干部群众的强烈愿望，更是维护了广大受灾群众的切身利益、保障受灾群众基本生存权利的需要。”<br />
<br />
 <br />
<br />
　&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;绵阳市委说法与记者所见反差很大 <br />
 <br />
<br />
　　安县发改局钟守平副局长９月２７日上午对中国经济时报记者介绍说，７月到８月之后砖厂老板开始上访，提出了各种要求，安县的书记、分管书记、县长与县内的砖厂开了座谈会。<br />
<br />
 <br />
<br />
　　而颜雪良、吴佳明、杨庆寿等砖厂老板说，“目前，一半砖厂停产，近两万多民工工资无法兑现，四万多民工面临失业，各厂占有的１８０００多亩土地中，接近一半是耕地，如今闲置，９０００多亩森林为建厂已经被砍伐，这个成本究竟该谁承担？”<br />
<br />
 <br />
<br />
　　他们提出，政府应该拿出可行的方案，对砖厂老板们提出的行内兼并重组和压缩产能提供有力的支持，而不应以“砖厂投资是市场化行为，政府不应负责”之类的言辞推脱。<br />
<br />
 <br />
<br />
　　对此，安县的那几位官员说，不止安县有砖厂的问题，安县恐怕不好自行出台什么政策。<br />
<br />
 <br />
<br />
　　绵阳市委宣传部向本报发来的邮件称：“全市现有６４１户制砖企业，基本都处于正常生产状态……现阶段我市制砖企业除极少数没有满负荷生产外，大部分制砖企业都是满负荷生产。”<br />
<br />
 <br />
<br />
　　这与中国经济时报记者的亲眼所见反差颇大。<br />
<br />
 <br />
<br />
　　该邮件表示，绵阳各县市区主要领导都对砖厂问题极重视，多次带队实地调研并采取措施：迅速核实调低砖厂的税收；迅速完善新建砖厂的各项手续（绝大多数新建砖厂都是边建边完善有关手续），加快完善办理新建砖厂各种手续的进程；积极拓宽销售渠道。<br />
<br />
　　<br />
