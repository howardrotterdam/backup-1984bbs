													<h2>刘晓原：艾未未有拒绝“聊天”的权利</h2>
						
						
																											<div id="postmessage_105086" class="t_msgfont">5月26日晚近十一时，我突然接到艾未未打来的电话。他问我，打110报警被带在派出所做完了笔录，警方是不是应给一个报警回执？<br />
<br />
&nbsp; &nbsp; 我赶紧问发生了何事，他简单说了几句。有警察到他母亲住处，想与他聊天了解情况。有一个“电饭煲”（艾未未语），没有出示证件，说话磨磨唧唧，艾未未一时火起，就打了110报警。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;艾未未与他们去派出所做了笔录，他要求派出所给一个报警回执，警方说这是不能给的。他不知这样做是不是合法，所以才打来电话咨询。<br />
<br />
&nbsp; &nbsp; 我解释一般后挂了电话，大概只过了十分钟时间，艾未未再次打来电话，说警察竟拦住他不让走，他狠踢了几脚派出所大门。原来艾未未进入派出所后，人家担心他会擅自离开而把门给关上了。艾未未再次打电话向110报警，警察见他发大火了赶紧把门打开。<br />
<br />
&nbsp; &nbsp;艾未未说，不清楚到底为何事要找他？他们来了后，也只说想聊聊。艾未未认为，双方不认识且没有共同语言，没有必要浪费时间。直性子的艾未未不懂转弯抹角，不知道“电饭煲”工作套路就是先有事无事地搭讪，到了一定火候才会正式给你出题。艾未未见他不爽快，不出示证件，只好选择报警了。<br />
<br />
&nbsp; &nbsp;艾未未的遭遇，一些维权人士都经历过。据说，套近乎式的工作方法，不会使人情绪产生对立，还可拉近双方心理的距离。想不到的是，这招在艾未未身上失效了。<br />
<br />
&nbsp; &nbsp;艾未未说，下次来最好带手铐吧。否则，没有功夫作闲谈。艺术家口气就是大，要是换了其他人，人家也就不客气了。<br />
<br />
&nbsp; &nbsp;某律师给我说起过一件事，他因参与了一个签名活动，被住所地的派出所找去聊天，时间选择在晚上近十二时，且事先不给你打招呼。半夜时分，人家咚咚地敲门说有事需要了解一下。某律师说请出示传唤证，人家说就是去聊聊天嘛什么传唤证呀？某律师不去，警察就不走。为了不影响邻居和家人休息，某律师还是去派出所聊了几个小时。<br />
<br />
&nbsp;&nbsp;警察值班陪人家聊天，除了工资还有加班费，第二天仍可以补休。找律师聊天了解情况，这也是属于咨询，按说可以收取律师费的。我开玩笑对某律师说，下次人家来找你聊天，可不能再提供免费咨询了。俗话说没做亏心事不怕鬼敲门，但真的有鬼敲上门了，你不怕也会有心理压力。据说，用这招来对付人还是很管用。<br />
<br />
&nbsp; &nbsp;还有更加损的一招，如果你是租房居住，人家可叫房东解除协议把你赶走，甚至赶出管辖范围才罢休。当然，对那些买了房的人，也是有办法对付。某律师是外地人，曾经被不明身份者拉到郊区打了一顿威胁他混出京城。<br />
<br />
&nbsp; &nbsp;当然，人家有时还是很文明，既不骂你，也不打人，还免费做你保镖。你去哪里只跟在后面，有时还派车送你。不知道底细的人，真以为你抖起来了，富到有钱来雇请保镖。<br />
<br />
&nbsp; &nbsp; 其实，艾未未是很随和的一个人，只要事前与他联系好了，找他聊天并不是难事。他既不会高高在上，也不会摆名人之谱， “电饭煲”不了解艾的性子，工作方法不对头，才搞得双方都不愉快。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;从法律上来看，公民有权拒绝聊天谈话，这是公民的基本权利。只要人家不愿意，强行找人“聊天”，就是在骚扰与侵权。<br />
