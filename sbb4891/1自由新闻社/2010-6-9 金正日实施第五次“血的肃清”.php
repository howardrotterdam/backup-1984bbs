北韩国防委员长金正日自20世纪90年代正式上台以来，每次遇到重大局面就通过“血的肃清”来维持权力。近来为追究货币改革失败的责任，他对劳动党计划财政部部长朴南基处以枪决等，共铲除了100多名干部。有分析认为，这是金正日第五次实施大规模肃清。韩国政府安全部门一位官员表示：“过去金正日一直利用 ‘肃清的恐怖’控制干部。但似乎此次在内部遇到不少反对意见。”<br />
<br />
1992年10月，金正日发现苏联海归派军官批判北韩体制，便铲除了20多人，并免去300多名海归派的职务。当时主导肃清的保卫局局长元应熙中将被破格晋升两级，成为大将。当时金正日刚刚上任最高司令（1991年12月），通过肃清苏联派军官掌握了军权。<br />
<br />
金日成逝世（1994年7月）后不久的1995年4月，北韩发生了“第六军团肃清”事件。有一些人认为第六军团（驻扎在咸镜北道）肃清运动是政变谋反事件，但普遍分析认为：“第六军团政治委员、咸镜北道党组织秘书、贸易公司总经理等20多人贪污外汇收入，扰乱指挥体系。”当时金正日派金永春（现人民武力部部长）大将前往形迹可疑的第六军团，冷酷地处决了涉案人员。据说，被处决的军人就达到数百人。凭借这一功劳，金永春晋升为总参谋长（1995年10 月）。<br />
<br />
金正日最具代表性的一次肃清运动，发生在1997年导致100多万名居民饿死的“苦难行军”时期。当时金正日给劳动党农业秘书徐宽熙扣上“美帝国主义间谍”的帽子，在平壤市民面前公开处决。这实际上是将粮食短缺的责任强加给徐宽熙。当时有2000多人被肃清，罪名是“重新调查朝鲜战争（韩国称‘6.25 战争’、‘韩国战争’）时期的行迹后发现有间谍嫌疑”。<br />
<br />
但因“间谍调查”事件，出现太多受害人（包括被处决者家属达数万人）后，民心开始恶化，金正日便于2000年处理了所有曾经参与间谍事件调查的数十名干部，罪名是“被权力蒙蔽了双眼，挑拨党和民众的关系，中了敌人的奸计”。<br />
<br />
金正日曾于2004年将时任劳动党组织指导部第一副部长张成泽驱逐出权力中心，2006年将部分引入市场经济体制的朴凤柱总理等人免除职务，但没有达到 “血的肃清”的程度。但随着今年初货币改革失败导致民心极度恶化，金正日便断然枪决追随其40年的亲信朴南基等人，再次启动大规模肃清运动。北韩内部消息人士称，政府方面声称干部贪污腐败导致民不聊生。<br />
<br />
但内部氛围却与以往截然不同。北韩的消息人士称：“如今北韩居民也已经发现问题。众多干部质疑‘我们到底做错了什么’，百姓则质疑‘肃清了又能怎么样’。”<br />
<br />
近来访问中国的北韩人当中有许多人认为“内部动荡从现在开始”。今年晚春气温低于正常水准，农作物冻害严重，化肥也几乎断供，预计今年的农业收成很差。韩国开发研究院(KDI)本月7日发表一份题为《2010年5月北韩经济动向》的报告称：“北韩的大米价格和汇率再次暴涨。”干部阶层也开始动摇。在各机关单位，如果没有供应粮食的能力，高层领导必须下岗。<br />
<br />
众多老百姓抱怨说：“连朴南基都要被枪决，你算什么？”阶级秩序也开始出现动摇。韩国高丽大学教授赵荣基表示：“在世袭继承过程中，可能会发生又一次的 ‘血的肃清’。”</div>

							
							
							
							
