													<h2>乌克兰总统呼吁国民清除共产主义污垢</h2>
						
						
																											<div id="postmessage_280851" class="t_msgfont">VOA记者: 白桦 | 莫斯科 2009年 10月 12日 <br />
<br />
扼要：乌克兰总统尤先科在悼念活动中发表讲话说，共产党的标志是屠杀的象征，乌克兰国民应该清除共产主义污垢，放弃带有共产党标志的纪念物和偶像，并把这些魔鬼式的标志仍进历史垃圾堆。<br />
<br />
乌克兰总统尤先科呼吁国民清除共产主义污垢，把带有共产党标志的纪念物和偶像仍进历史垃圾堆。<strong>尤先科还表示，恢复历史的真相和公正是民族复兴的基础。</strong><br />
<br />
位于乌克兰西南部的伊万诺-弗兰科夫斯克州星期天举行活动，悼念当年苏共斯大林政权镇压迫害的遇难者。<br />
<br />
在当地的一个名叫杰米亚诺夫狭口的地方安葬着500多具遇难者的尸体。1941年6月苏联红军从伊万诺-弗兰科夫斯克州撤退时，苏共秘密警察夜间在杰米亚诺夫狭口处决了1000多名政治犯。1989年，当地的纪念碑人权组织展开发掘工作，把找到的500多具遇难者的遗骨重新安葬在一起。遇难者中有妇女、老人甚至正在哺乳中的婴儿。<br />
<br />
<strong>*共产党迫害遇难者博物馆开馆*</strong><br />
<br />
在墓地附近，刚刚建成的共产党迫害遇难者博物馆当天宣布开馆接待参观者。博物馆列出了当年参加屠杀处决行动的377名苏联秘密警察的名单。<br />
<br />
当地的天主教会以及东政教会星期天为此举行了安灵弥撒活动。乌克兰总统尤先科、乌克兰国家安全委员会秘书长纳利瓦伊前科等高级官员、以及遇难者的亲属等等参加了星期天的纪念活动。<br />
<br />
<strong>*尤先科：把共产党魔鬼标志仍进垃圾堆*</strong><br />
<br />
乌克兰总统尤先科在悼念活动中发表讲话说，<strong>共产党的标志是屠杀的象征，乌克兰国民应该清除共产主义污垢，放弃带有共产党标志的纪念物和偶像，并把这些魔鬼式的标志仍进历史垃圾堆。</strong><br />
<br />
<strong>尤先科还抨击试图为斯大林辩护以及否认大饥荒的行为恬不知耻。</strong>他说，那些遇难者中有神职人员、教师、大学生、以及当地的知识界人士，这些人被屠杀就是因为他们是乌克兰人；人们应该记住那些参加当年共产党迫害行动的刽子手和卖国贼，只有恢复历史真相和公正，乌克兰民族才能复兴。<br />
<br />
尤先科说，三个波罗的海国家以及波兰都已经立法把斯大林主义同纳粹法西斯等同，乌克兰迟早有一天也会这样做。<br />
<br />
研究斯大林政治迫害历史的俄罗斯纪念碑人权组织的积极分子别良金说，在一些前苏联共产党国家，清除共产党的标志不是一件简单的事情，比如莫斯科红场上至今保留列宁墓。他说：“这实际上涉及执政者如何评价十月革命，如何评价布尔什维克当年的所作所为，正因为如此，在俄罗斯国内仍然有几千个列宁的塑像保留在那里。”<br />
<br />
几天前，乌克兰第一夫人叶卡杰林娜.尤先科表示，俄语系居民聚居的乌克兰东部地区仍然保留着许多产党领袖的塑像，这些塑像不拆除，当地居民就无法指望过上富足生活。<br />
<br />
<strong>*人权人士：共产党制造大饥荒*</strong><br />
<br />
乌克兰基辅的纪念碑人权组织积极分子普罗特尼科夫说，由于经历过大饥荒，斯大林政权的迫害以及切尔诺贝利核事故等等，其实乌克兰人更憎恨共产党政权。他说：“比如乌克兰在20世纪总共发生了三次大饥荒，这些大饥荒都是共产党统治的结果。在这之前，乌克兰历史上从未发生过大饥荒，而且有欧洲粮仓之称的乌克兰也不可能发生大饥荒。”<br />
<br />
尤先科总统今年5月在首都基辅郊外举行的另一次悼念斯大林政权迫害遇难者的活动中，也曾呼吁在全国范围彻底拆除共产党的标志性纪念物。尤先科当时透露，<strong>2007年到2008年期间，乌克兰总共拆除了400多个共产党领袖的塑像，并重新命名了3000多个以共产党领导人命名的地区和街道。</strong> <br />
