																											<div id="postmessage_86481" class="t_msgfont">奥巴马总统就“世界新闻自由日”发表声明<br />
<br />
 <br />
<br />
白宫于5月1日公布了奥巴马总统就“世界新闻自由日”（World Press Freedom Day）发表的声明，以下是声明全文的中译文，由美国国务院国际信息局（IIP）翻译：<br />
<br />
――――――――――――――――<br />
<br />
白宫<br />
<br />
新闻秘书办公室<br />
<br />
即时发表<br />
<br />
2009年5月1日<br />
<br />
奥巴马总统“世界新闻自由日”声明<br />
<br />
每年的5月3日是“世界新闻自由日”，它提醒我们所有人这一核心自由的极端重要性。我们在这一天宣扬新闻工作者为揭露滥权所发挥的不可或缺的作用，同时它警告我们，正在有越来越多的新闻工作者在向公众作日常新闻报道时，被死亡和铁窗扼杀了声音。<br />
<br />
虽然世界新闻自由日自1993年以后才被确立为每年一度的纪念日，但它在国际社会有着深远的根源。1948年，在世界人民摆脱了第二次世界大战的恐怖后，各国认识到，需要将人人“有权享有主张和发表意见的自由；此项权利包括持有主张而不受干涉的自由，以及通过任何媒介和不论国界寻求、接受与传递消息和思想的自由”的基本原则纳入《世界人权宣言》(Universal Declaration of Human Rights)。<br />
<br />
尽管世界认识到新闻自由无可非议的极端重要性，但新闻工作者仍时常陷入危境。自大约16年前首次确立这个纪念日以来，有692位新闻工作者遭到杀害。其中，只有三分之一是与战地报道有关；大部分的遇害者是报道本国犯罪、腐败和国家安全等问题的地方记者。除了这一令人痛心的数字外，每年还有更多人遭到恐吓、审查和任意关押――他们的罪名不过是热衷追求真相，并坚信一个自由社会取决于一个知情的公民群体。全球每个角落都有新闻工作者受到关押或遭到肆意骚扰：从阿塞拜疆到津巴布韦，从缅甸到乌兹别克斯坦，从古巴到厄立特里亚。这种令人不安的现实通过斯里兰卡的提塞纳亚伽姆（J.S. Tissainayagam）以及中国的石涛和胡佳得到反映。我们也尤其担忧目前在海外遭到关押的我国公民，其中包括被伊朗关押的萨贝里（Roxana Saberi）和被北韩关押的李云娜（Euna Lee）和凌志美（Laura Ling）。<br />
<br />
今天，我向全世界所有为揭露真相和加强问责而奋斗的男女新闻工作者表示声援，并向他们致敬。此时此刻，我的脑海里回响着托马斯・杰斐逊的一段话：“民意是我们政府的基础，先于一切的目标应当是维护这一权利；如果让我来决定，我们是要一个没有报纸的政府还是没有政府的报纸，我将毫不犹豫地选择后者。”<br />
<br />
（完）<br />
