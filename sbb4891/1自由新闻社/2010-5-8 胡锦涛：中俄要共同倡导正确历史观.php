<br />
全文如下：<br />
<br />
尊敬的俄罗斯老战士们，<br />
<br />
亲爱的同志们、朋友们：<br />
<br />
你们好！<br />
<br />
很高兴来到莫斯科出席伟大卫国战争胜利65周年庆典。5年前，我曾在这里同部分参加过中国人民抗日战争东北战场战斗的俄罗斯老战士见面。今天再次和新老朋友见面，感到十分亲切。首先，我代表中国政府和人民向各位老战士致以诚挚的问候和崇高的敬意！<br />
<br />
70多年前爆发的第二次世界大战，给世界和平带来空前灾难。世界各国人民同仇敌忾，携手反击法西斯侵略者，保卫世界和平。苏联军民浴血奋战，从保卫家园到攻克柏林，2700万苏联军民献出了宝贵生命，谱写了惊天动地的壮丽篇章。英雄的苏联军民不仅捍卫了祖国的主权和尊严，而且为世界反法西斯战争胜利作出不可磨灭的历史贡献。<br />
<br />
与此同时，在第二次世界大战亚洲太平洋主战场上，中国人民同日本侵略者进行了8年艰苦卓绝的斗争，3500万中华儿女为国捐躯。中国人民以不屈的抗争和顽强的战斗给日本侵略者以沉重打击，粉碎了日本军国主义称霸亚洲的企图，为赢得世界反法西斯战争作出重大贡献。<br />
<br />
我们不会忘记，在这场极为残酷的较量中，包括今天在座老战士在内的大批苏联红军远赴中国东北战场，同中国军民肩并肩对日作战，为中国人民夺取抗日战争最终胜利作出重要贡献。许多优秀俄罗斯儿女在中国大地上献出宝贵生命。中俄两国人民在反法西斯战争中结下深厚友谊。这种用鲜血和生命凝结的友谊，是中俄战略协作伙伴关系的坚实基础，也是中俄世代友好的坚实基础。几十年来，我们始终深切缅怀为中国人民抗日战争胜利献出生命的中俄英烈，永远铭记他们用生命铸就的历史功勋。<br />
<br />
在伟大卫国战争胜利65周年之际，我们深感今天的和平来之不易，值得倍加珍惜。在当前复杂多变的国际形势下，中俄两国应该以史为鉴，面向未来，加强战略协作，重点在以下几个方面共同作出努力：<br />
<br />
<strong>我们应该共同倡导正确历史观。中国和苏联为世界反法西斯战争胜利作出了卓越贡献，这是铁的事实。中国人民高度评价苏联军民在世界反法西斯战争中发挥的重大作用，始终牢记苏联军民在中国人民抗日战争决战关头向我们伸出了援助之手。</strong><br />
<br />
我们应该共同倡导互信、互利、平等、协作的新安全观。虽然第二次世界大战已经结束65年了，但我们必须铭记那场战争留下的沉痛教训。当今世界仍不太平，和平与发展面临严峻挑战。我们呼吁国际社会树立和践行新安全观，增加相互信任，照顾彼此利益，坚持友好协商，尊重多样文明，共谋合作发展。<br />
<br />
我们应该加强战略协作。作为世界大国和联合国安理会常任理事国，中俄有责任积极倡导世界多极化和国际政治民主化，积极推动建立更加公平合理的国际政治新秩序。我们要在国际事务中密切合作，共同维护世界和地区和平。<br />
<br />
同志们，朋友们：<br />
<br />
当今世界正处在大发展大变革大调整时期。我相信，中俄两国人民将同各国人民一道，牢记历史，开创未来，为推动建设持久和平、共同繁荣的和谐世界而不懈努力！<br />
<br />
祝愿各位老战士健康长寿，幸福安康。<br />
<br />
谢谢大家。<br />
