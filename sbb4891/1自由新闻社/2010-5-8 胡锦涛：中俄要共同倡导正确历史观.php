<br />
中新网5月10日电 据俄新网9日报道，俄罗斯总统德米特里・梅德韦杰夫在会见中国国家主席胡锦涛时表示，相信中俄两国的睦邻友好关系将世代相传。<br />
<br />
梅德韦杰夫在同胡锦涛举行双边会晤时感谢他出席卫国战争胜利日庆典活动。<br />
<br />
梅德韦杰夫指出，他经常与胡锦涛见面，4月份在“金砖四国”峰会上曾见过面，6月份还将在上合组织峰会和加拿大二十国集团峰会上会晤。<br />
<br />
梅德韦杰夫说：“如此频繁的会晤是发展俄中睦邻友好和战略伙伴关系的保证。我相信，这种关系将世代相传。”<br />
<br />
胡锦涛感谢梅德韦杰夫邀请他来访问莫斯科。他还对俄罗斯在不久前中国发生地震灾时提供的人道主义援助及俄罗斯总统的慰问表示衷心感谢。胡锦涛还对俄罗斯积极参加不久前开幕的上海世博会向梅德韦杰夫表示感谢。<br />
<br />
胡锦涛称中俄在二战历史问题上立场一致<br />
<br />
国家主席胡锦涛8日在莫斯科会见俄罗斯总理普京。胡锦涛指出，在第二次世界大战历史问题上，中方同俄方立场一致。双方要加强沟通和协调，坚决维护历史的真实性和严肃性。中方愿同俄方一道努力，共同举办一系列活动，庆祝世界反法西斯战争胜利65周年。<br />
<br />
(本文来源：重庆日报 )</div>

							
							
							
							
