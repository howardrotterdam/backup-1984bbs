																											<div id="postmessage_821987" class="t_msgfont"><img src="http://www.people.com.cn/mediafile/pic/20100909/21/11513656115563490165.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
近日，温家宝来到深圳皇岗社区看望居民时，与居民们一同做柔力球运动。<br />
<br />
<br />
　　新华社记者刘卫兵摄<br />
<br />
　　“我也是球迷！”在一次中德两国总理记者招待会上，温家宝总理如是说。<br />
<br />
　　作为球迷，温总理不仅会“半夜起来看球赛”，还常亲自玩球。<br />
<br />
　　2006年，接待德国总理默克尔时，他拿起柔力球拍，上来就当起了教练；2007年去日本访问，他穿上棒球服，一打就是半小时；2008年考察奥运篮球馆，他跑步上篮，一投就是5个球；2010年8月，去深圳考察，他又与当地居民们一起打起了柔力球。<br />
<br />
　　棒球、篮球，这都是温总理年轻时的强项，如今，他常常玩柔力球。<br />
<br />
　　从激烈的对抗性球类，到蕴含太极智慧的柔性球类，这里面，挺有健身门道。<br />
<br />
　　年轻玩棒球篮球<br />
<br />
　　2008年8月3日，温总理在北京五棵松奥林匹克篮球馆跑步上篮、大秀球技。报道刊出后，国人哗然，原来温总理还有这一手，跑步上篮姿势颇为标准优美。<br />
<br />
　　总理母校南开中学袁化老师透露：“总理读书时就是个篮球高手！”<br />
<br />
　　除了篮球，年轻时，温总理还擅打棒球。2007年访日时，就与京都立命馆大学的学生一起打棒球，在日本赢得了“35号球员”的雅号。<br />
<br />
　　北京市科学健身讲师团秘书长、运动健康专家赵之心分析说，棒球和篮球都是集体性、对抗性很强的竞技球类运动，对身体素质的要求很高，比如打篮球，经常会发生身体的碰撞，必须有短跑的爆发力、长跑的耐力和良好的弹跳能力。棒球同样如此。在竞技运动上，25岁的运动员就是一个老运动员了。因此，这样的运动适合年轻人。<br />
<br />
　　反过来讲，年轻时，打打篮球和棒球也是对自己身体的一种淬炼，让身体的肌肉、心肺功能达到顶峰，为以后奠定一个良好的身体基础。<br />
<br />
　　年长练练柔力球<br />
<br />
　　赵之心指出，温总理打篮球、玩棒球可以说是兴之所至。毕竟他已经是六十多岁的人了，反应能力、弹跳能力等都会下降，玩柔力球更适合他这个年龄的人。<br />
<br />
　　柔力球又叫太极柔力球，听名字就知道是一种动作舒缓的运动。别看只是胳膊在动，其实它是一种全身性的运动，可以使颈、肩、腰、腿得到均衡全面的发展。<br />
<br />
　　赵之心说，年轻时候靠运动“储蓄”肌肉，年老时候就要靠运动延缓肌肉退化。柔力球就能达到这个目的，锻炼小肌肉群，增强肌肉控制能力，防止肌肉退化，还能增强身体的协调能力。<br />
<br />
　　从公开的报道看得出，温总理柔力球玩得很好。可见，温总理的身体很好，另一方面猜得出，温总理工作之余可能在玩柔力球。</div>

							
							
							
							
