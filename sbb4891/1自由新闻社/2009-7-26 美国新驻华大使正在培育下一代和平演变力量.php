																											<div id="postmessage_179585" class="t_msgfont">这么说，那还研究个鬼的历史啊。<br />
--------------------------------------<br />
正因为差，所以要研究源头差在那里。不过既然社会进步到了这个地步，历史科学早已引入统计学和交叉学科研究，那么今天的读书人也不该只看史书，至少要先运用各种技术尽量还原历史，而不是面对祖先自汉代开始毁林毁牧、破坏水土的胡闹历史，还照古书高歌“古代中国天人合一”；面对“菜人”价格低于猪肉的“父子相食”、“夫妇相食”历史，还怀念先人之仁爱礼让、井然有序。<br />
<br />
<br />
当下中国一片道德败坏，历史空泛而不具体，现实无情而残酷，你这种观点今天很有市场，刚才那么并不是说讽刺你。<br />
------------------------------------------------------------------------------------------------------------------------------------------<br />
你这话讲给意志薄弱的人可能有市场，耶和华见证人对穷人传教，一般开篇都这一句：当下社会道德败坏啊，人心不古啊。<br />
<br />
一直认为香港存在很多问题，但不认为这里道德一片败坏。恰恰相反：70年代后社会伴随财富的累积、管治的提升、教育的普及，在道德上明显有进步。我自己也深有体会：因为马虎，手机丢了几次，都被人拾到，等了我很久，还帮我充上电，其他亲身经历多不胜数。商业道德上的飞跃更是不言说，数千年来中国官僚对商业的盘剥和权力寻租，在这消失无踪。<br />
<br />
<br />
中国的传统文化已经断代了差不多50年，风俗，信仰，家规，行规，文化，早已荡然无存，这些都是些细节的东西，细细的堆砌起一个传统向现代过渡的中国。<br />
现代人都不相信中国人有信仰。<br />
而一个以前连握笔和拿筷子，接人待物都极讲规矩的民族，会没有信仰？<br />
乡间没有一个警察，却井然有序的民族，会没有信仰？<br />
-------------------------------------------------------------------------<br />
刚才所说，香港市民的道德进步，恰恰是英国文化对中国传统观念（还包括你说的那些礼仪）冲击下取得的。电视台的怀旧节目，时常请到一些老人，讲以前家族的礼仪、行业的礼仪，这些繁文缛节今天大都消亡了，但香港人今天的公德心、个人信用、家庭教育、守法遵纪、日常礼貌和最重要的------公平观念，却有了长足的进步。<br />
<br />
内地现在道德不好，说归因于这些细节的消亡，或许还有人追捧。但香港恰恰提供了一个传统华人社会道德进步的范例，证明了道德之源不在复古，不在礼仪，更不在陋规。<br />
