																											<div id="postmessage_430887" class="t_msgfont">　 “你妈的B，你烂狗日的，你小报记者敢跟老子大人物嗦”、“老子马上电信局查你”……在长达三分多钟的电话采访里，贵州省赫章县珠市彝族乡党委书记陈跃东一直不停地破口大骂记者，不但侮辱威胁，骄横跋扈，还极尽世间污言秽语。这一切只因为――<br />
<br />
&nbsp; &nbsp; 2009年12月10日，记者接到贵州省赫章县珠市乡群众举报：该县珠市彝族乡党委书记陈跃东与当地一名女教师发生一起离奇车祸：夜晚十点过钟，这两人乘坐着陈跃东的公车从荒僻的乡村道上翻到坡下，村民们前来救助时发现女教师竟然赤裸着下身，没有穿裤子。一时间种种猜疑在赫章街坊流传，事情在当地弄得沸沸扬扬。<br />
<br />
&nbsp; &nbsp; 为进一步弄清事情真相，记者于2009年12月21日出发前往贵州省赫章县进行实地采访。<br />
<br />
离奇交通事故：<br />
<br />
<br />
&nbsp; &nbsp; 和乡党委书记一起翻车的女教师没穿裤子　<br />
<br />
&nbsp; &nbsp; 事发地点在珠市乡毗邻的赫章县水塘堡彝族苗族乡一条偏僻村道上。此条道路又烂又窄，平时很少有车辆经过。<br />
<br />
&nbsp; &nbsp; 2009 年12月22日上午，记者走进赫章县水塘堡彝族苗族乡派出所办公室，在一位警官拿出的一份“出警记录”上，记者看到：10月29日晚上11点左右，水塘堡乡派出所接到该乡永康村大捞地组村民马德科电话，报称有一辆汽车从该村二台坡山上翻至大捞地上峰氧粉厂后面的土地里，可能有人员伤亡。接到报警后，指导员罗饶富带领警察王定乾、王铁岭、协勤石陶于10月30日凌晨零时13分赶到报警现场，立即组织当地群众展开搜救行动，在半山中找到一名中年男子与一名中年女子，两人昏迷。将两名伤者抬至公路，组织车辆送赫章县医院抢救后，水塘派出所人员回所。<br />
<br />
&nbsp; &nbsp; 根据这一出警记录，记者前往水塘堡乡永康村大捞地组进行实地采访。住在上峰氧粉厂后面的村民安某某告诉记者：10月29日晚上10点过钟，家人正准备睡觉，突然听到二台坡山上传来很大的响声，似乎有东西从山上往下滚，大家出门一看，看到一辆亮着灯的车正从山上往山下滚来。<br />
<br />
&nbsp; &nbsp; “我说‘出事了’，赶紧叫上家人打着电筒朝车子滚落的方向跑去，跑到已经滚到我家门前不远处庄稼地里的车身前一看，车里没人。紧接着就听到半山上有个女的在喊救命，我们就又往山上爬。爬到半山看到一对中年男女在那里，那个中年男的是珠市乡党委书记陈跃东，因为之前陈跃东在我们水塘堡乡任过党办主任，所以我们都认识他。当时陈跃东还能自己走动，似乎问题不大，女的要严重些，浑身是血，下身连裤子都没穿，女的见我们打着电筒，第一句话就是‘不要点电筒，害羞得很’。我赶紧叫我侄儿把外裤脱下来给女的穿上。然后我们才把那女的抬下山放到平地上。”安某某说。<br />
<br />
&nbsp; &nbsp; 当时，参与救人的村民询问两位伤者“半夜三更来山上干什么”，对方并不正面回答，男的告诉村民“还有个驾驶员”，村民们听说后，打起电筒要找，男伤者又说 “驾驶员已经跑了”。“我们就觉得奇怪，因为从看到汽车翻滚下山，到我们立即爬上山，根本就没有看到除陈跃东和那女的外还有第三个人。”<br />
<br />
&nbsp; &nbsp; 村民们说，当时男伤者说自己是做生意的，但因为对方在本乡工作过，所以他们立即认出了他“就是珠市乡陈书记”，而女的是珠市乡一学校教师，珠市乡中心校教务主任的老婆，叫卢某某。<br />
<br />
&nbsp; &nbsp; 村民们说，翻车的这条简易公路因为又窄又烂，平时很少有车过，更不是珠市乡通往县城的路，最让人奇怪的是那个女的为什么会裤子都没穿？“太奇怪了，一定没有好事。”而肇事车，村民们描述说，那是珠市乡政府里的一辆好车，黑色的。“看那样子最少也得管几十万，在它翻下来的那块地里都躺了好几天才被人弄走了。 ”<br />
<br />
&nbsp; &nbsp; 据村民反映，事发当天，本来还能自己走动的陈跃东，看到水塘堡乡派出所的罗饶富指导员等人来后，突然躺到地上一直哼。看到这情景，罗饶富与其他3位警察就组织村民把陈跃东和女教师抬到公路上，后来是陈跃东的哥哥开车来把陈跃东和卢某某运走的。<br />
<br />
&nbsp; &nbsp; 为进一步核实情况，2009年12月22日下午，记者决定直接采访当事人陈跃东。 <br />
<br />
&nbsp; &nbsp; 乡党委书记大骂记者：“你个小报记者敢跟老子大人物嗦”<br />
<br />
&nbsp; &nbsp; 意想不到的是，电话刚一接通，听说是来采访的记者，陈跃东立即破口大骂：“ 你妈的B，你烂狗日的，你个小报记者敢跟老子大人物嗦。”<br />
<br />
&nbsp; &nbsp; 在陈跃东辱骂记者的3分多钟时间里，记者至少6次以上向陈跃东亮明身份，但陈跃东照样骂不绝口，于是在陈跃东骂了近1分钟后，记者录下了陈跃东后面辱骂记者的话。陈跃东的语言极尽世间污言秽语，叫人听到都面红耳赤。这话不要说一个乡党委书记，就是一位稍微有点素质的老百姓都难以骂出口。<br />
<br />
&nbsp; &nbsp; 记者随后来到赫章县相关部门核实情况。在赫章县委常委、纪委书记陈石光的办公室，记者表明来意后，陈石光说：“已经找陈跃东谈过话了，并且免去了陈跃东珠市乡党委书记的职务”，对陈跃东辱骂记者一事，陈石光表示会再找陈跃东谈话的。记者多次要求看一下赫章纪委对陈跃东处理的相关文件，但是被拒绝了，陈石光多次对记者说：“已经处理了就不要再报(报道)了。”<br />
<br />
&nbsp; &nbsp; 赫章县委常委、宣传部长李文均对记者表示，他真正知道陈跃东的事是在2009年12月20日，之前只知道陈跃东开车出事了，是和一个女的在一起，陈跃东本人并没有多大事，女的摔得严重些。直到12月20日，赫章县委开常委会听县纪委关于陈跃东的调查报告时，才知道事情的经过。<br />
<br />
