社会主义道德及其特征<br />
<br />
社会主义道德，是以社会主义公有制为主体的经济基础的反映；是在无产阶级自发形成的朴素的道德基础上，以马克思主义的世界观为指导，由无产阶级自觉地培养起来的道德。是以为人民服务为核心，以集体主义为原则，代表无产阶级和广大劳动人民根本利益和长远利益的先进道德体系。它属于共产主义道德体系。<br />
<br />
社会主义道德是在社会主义社会经济文化基础上形成的道德历史类型。社会主义道德与共产主义道德既有着密切联系，又有一定的区别。社会主义道德和共产主义道德都是建立在公有制基础上的道德体系。其基本精神是一致的。但由于公有制发展程度不同，两种道德又有不同的要求和历史使命。社会主义社会是共产主义社会的初级阶段，是从私有制向公有制，以及公有制程度不断提高的阶段。因此，要根据本阶段历史任务和人民群众思想觉悟提出社会主义社会道德的内容和要求。<br />
