台港示威 声讨富士康<br />
<br />
官方工会撤回 员工自组保权益<br />
<br />
<img src="http://thumbsnap.com/i/WSCyd6xm.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
台湾劳工团体昨到富士康母公司鸿海集团抗议，为深圳富士康跳楼员工伸张正义。路透社<br />
<br />
深圳富士康员工跳楼自杀潮震撼两岸三地，各地劳工团体纷纷声讨。台湾劳工团体昨到富士康母公司鸿海集团总部踩场；本港左、右两派劳工团体亦往中联办请愿；内地官方全国总工会派员参加中央调查组进驻富士康调查。消息指，富士康现有工会由深圳官方派驻的，当局已要求深圳撤回官派工会，由富士康员工自组工会保障权益。<br />
<br />
深圳富士康今年发生 12宗工人跳楼自杀，造成 10死 2伤，另有一人割脉自杀受伤获救。昨午再传有工人企图跳楼被阻止，大批港台记者赶往工厂采访，但未能证实。<br />
<br />
高喊「资本家偿命」<br />
<br />
在台北，数十名劳工团体代表昨聚集台北鸿海总部外，抗议其下属的深圳富士康屡逼出人命。团体打出「血肉何价」、「身心健康变泡影，青春年岁竟成梦」等标语，高喊「资本家偿命」、「生产线杀人」等。请愿团体代表指，富士康赚那么多利润，现在那么多人跳楼后，宣布加薪两成，「你到底在收买人心还是真的关注悲剧？」她说，那 13个年轻人在没出路下自杀，富士康「到底有没有诚心忏悔？」<br />
<br />
本港工会昨也到中联办请愿，要求内地政府部门介入富士康员工自杀事件，保障工人权益。港九工团联合总会的代表质疑，富士康涉嫌违反中国劳动法强迫员工长期加班。代表又指，内地政府应责令富士康实时停产整顿，保障劳动者权益。他们又希望富士康员工揭发雇主违反劳动法的行为。工联会则要求内地相关部门介入，评估富士康员工的工作压力是否过大，以及协助员工成立一个代表劳方的工会。工联会表示，正联络在深圳富士康工作的港人，了解他们有没有需要提供协助。<br />
<br />
内地全国总工会的代表昨会同公安部、人力资源和社会保障部等组成中央调查组，抵深圳后进驻富士康至少一周进行调查。消息指，调查重点是富士康用工现状、管理模式及劳资关系等。调查组要求深圳市撤回官派工会，并协助富士康工人建立自己的工会，以利保障工人利益。<br />
<br />
<img src="http://thumbsnap.com/i/dHN4wrId.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
鸿海集团派出员工阻挡示威者进入集团大门。路透社<br />
<br />
<img src="http://thumbsnap.com/i/e6t82j0w.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
本港港九工团代表昨在中联办门前请愿，要求内地当局下令深圳富士康停产。李家皓摄<br />
<br />
苹果重新评估工作环境<br />
<br />
此外，继苹果（ Apple Inc.）、惠普（ Hewlett-Packard Co.）及戴尔（ Dell Inc.）后，富士康另外两名大客户诺基亚（ Nokia Corp.）和索尼（ Sony Corp.）昨日也发表声明指，关注富士康员工跳楼事件，公司将重新评估富士康的工作环境。委托富士康生产 Wii游戏机的任天堂昨日亦表示，已从今年初调查富士康内的工作环境，及调查其管理层是否遵守由任天堂定立的工作环境条件指引。<br />
<br />
富士康工人跳楼事件也引起台湾政坛关注，立法院长王金平昨表示，富士康事件难免会令其它公司不安，政府应伸出援助之手。他表示两岸应一起合作，赶快平息事件。民进党立委高志鹏也称，若富士康事件被炒大，对台商「将是前所未有的灾难」。<br />
<br />
另有厂方消息称，富士康大老板郭台铭前日由台湾乘专机再飞回深圳后，曾向高层声言不把问题解决好不回台湾。有富士康工人则发起今日戴蓝丝带在厂内游行。<br />
<br />
本报记者/台湾《苹果日报》/中央社</div>

							
							
							
							
