													<h2>北大附中50年校庆 毛新宇当选杰出校友</h2>
						
						
																											<div id="postmessage_857145" class="t_msgfont">2010/10/07 00:00&nbsp; &nbsp; 来源：YNET.com 北青网&nbsp;&nbsp;北京青年报&nbsp; &nbsp; 赵卓 <br />
<br />
　　本报讯 昨天，北京大学附属中学迎来50周年校庆，白发苍苍的老校友和风华正茂的年轻校友齐聚校园。学校也为这次校庆特意设计手拉手LOGO，并在国内中学率先推出“毕业戒指”。50名毕业生当选杰出校友，毛泽东唯一的嫡孙毛新宇少将也入选其中。 <br />
<br />
　　昨天，该校从上世纪60年代起的历届校友纷纷汇聚母校。北大博士生导师饶戈平、加拿大滑铁卢大学教授李明、凤凰卫视主播陈晓楠等知名校友也参加校庆，谈及附中对他们人生深深的影响。著名歌手、水木年华组合之一的缪杰也来到现场，80年代末入学的他称自己是“90”后。鲜为人知的是，缪杰是北大附中第一届实验班学生，北大附中理科第二名，但他也自嘲说“该干的坏事都在北大附中干了。” <br />
<br />
　　校庆现场，公布了北大附中校友会评选的50年50名杰出校友，这其中既有商界精英新浪董事长汪延，艺术界精英中央美术学院副院长徐冰，也有学术界、政界、文艺界的杰出人士。毛泽东唯一的嫡孙毛新宇少将也入选其中，他1985年进入北大附中学习。 <br />
<br />
　　校庆活动上，该校的毕业生还获颁了毕业戒指。戒指造型外观像冠，如同延续古时冠礼学子的礼仪。戒指顶端刻着学生的姓名，底部是学生毕业年份和班级等个性化信息，侧面是校徽和校训。 <br />
<br />
　　校庆活动上，戒指由从上世纪60年代到新世纪头10年的历任校长为每个年代的毕业生代表颁发。北大附中现任校长王铮表示，希望今天之后，毕业戒指可以作为一个传统延续下去，以后的毕业生都能在毕业典礼上获得属于自己的毕业戒指。</div>

							
							
							
							
