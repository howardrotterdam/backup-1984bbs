													<h2>舟曲高考降30分录取 97%考生能上大学</h2>
						
						
																											<div id="postmessage_783463" class="t_msgfont">2010年08月15日 07:21:53 　来源： 中国青年报 【字号 大小】【留言】【打印】【关闭】&nbsp;&nbsp;<br />
 甘肃省舟曲县县长迭目江腾今天晚间与中国青年报网站“中青在线”连线时说，甘肃省有关部门决定，今年高招专科一批文、理科在参照线基础上，对舟曲灾区考生各降30分录取。同时，专科二批按照文科220分、理科210分进行投档录取。这样，在今年参加高考的800多名舟曲籍考生中，将有97%的考生能入大学大专。<br />
<br />
&nbsp; &nbsp; 当地负责人说：“这是以人为本的政策。很多孩子爹妈没了，家没了，他们得到读书机会，是条生路。”<br />
<br />
&nbsp; &nbsp; 同时，对于目前高校在读和拿到高校录取通知书的舟曲籍学生，如果父母都去世或一方去世，政府考虑给予适当的经济支持，供他们读书。迭目江腾告诉记者，舟曲灾区的学校将推迟10天左右开学，目前灾区“缺老师，急需活动板房”。<br />
<br />
&nbsp; &nbsp; 他说：“县城关一小的建筑倒了，城关二小也被淹没了，3600名小学生没地方去读书。据初步计算，学校和受灾群众过冬安置用的板房需求量大约为18万平方米。”<br />
<br />
&nbsp; &nbsp; 迭目江腾介绍说，舟曲县一中的3040名学生已转移到兰州等地，异地办灾区学校。等学校恢复重建后，他们再回来。<br />
<br />
&nbsp; &nbsp; 迭目江腾告诉记者，舟曲下学期开学将缺50~60名教师。政府正争取兰州和甘南藏族自治州的援助，也欢迎有教学经验的全国志愿者支援。（邓江秀 记者王尧）</div>

							
							
							
							
