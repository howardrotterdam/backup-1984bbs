<br />
在谈及中国时，奥巴马表示，中国快速发展不代表是对其他国家的威胁，美中紧密合作对两国都更加有利。他说：“我们欢迎中国在世界舞台上扮演越来越重要的角色，中国在经济发展的同时中国的责任也在增大。”<br />
<br />
他又指出，中国的崛起将会有助全球发展。美国不谋求遏制中国，并欢迎中国在世界上发挥更大的作用。<br />
