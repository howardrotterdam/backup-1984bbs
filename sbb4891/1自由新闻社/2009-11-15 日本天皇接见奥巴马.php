													<h2>奥巴马向日本天皇“卑躬屈膝”被指“叛国”</h2>
						
						
																											<div id="postmessage_333760" class="t_msgfont">奥巴马向日本天皇鞠躬 “卑躬屈膝”引骂声<br />
<br />
2009年11月19日08:23<br />
<br />
奥巴马向天皇深鞠躬遭批评<br />
<br />
　　美国总统贝拉克・奥巴马访日期间会见日本天皇时,向后者深鞠躬近90度,招致美国媒体和保守派批评。他们指责奥巴马身为国家元首行为不妥,看起来“低声下气”,有向日本天皇“卑躬屈膝”之嫌。<br />
<br />
　　面对一片骂声,美国国务院17日回应说,奥巴马这一动作仅仅是为表示尊敬。<br />
<br />
　　鞠躬挨批<br />
<br />
　　奥巴马14日中午在日本皇宫与明仁天皇和皇后会面。他微笑走上前去,一边和明仁天皇握手,一边深深鞠躬。鞠躬幅度接近90度。随后,奥巴马和日本皇后美智子握手致意。<br />
<br />
　　这一举动招致美国媒体和保守派“炮轰”。《洛杉矶时报》一篇博文题目写道:“他还能低到什么程度？”美国微软―全国广播公司则称这一动作“尴尬”,因为鞠躬时不应有身体接触。另一篇网络文章甚至称奥巴马深鞠躬为“叛国”。<br />
<br />
　　华盛顿礼仪学校校长帕梅拉・艾尔灵说,虽然奥巴马鞠躬明显是为表示尊敬,但天皇夫妇并未期望一名西方国家领导人作出这种举动。她看过视频后,觉得天皇夫妇看到奥巴马行大礼“有些不舒服”。<br />
<br />
　　“一国国家元首向另一国国家元首鞠躬不是正确行为。这不太合适。他不应向别国元首鞠躬,”艾尔灵说,“当你代表美国时,每个举动都有含义。人人都看得出来。这更多表现出一种低声下气的姿态。”<br />
<br />
　　政府澄清<br />
<br />
　　面对批评声音,美国国务院17日发表声明说,奥巴马深鞠躬只是为向天皇表示尊敬,别无他意。<br />
<br />
　　美国国务院指出,白宫通常在总统出访前向礼宾司司长办公室征求建议,而后作出安排。声明中说:“总体来说,礼仪就是指尊重东道国传统和风俗习惯。总统这样做仅仅是为表示尊敬。”<br />
<br />
　　美国智库和平研究所高级研究员约翰・帕克赞同上述观点。他认为,在正式场合中,向一国君主鞠躬是一种表示尊敬的传统礼仪。不过,帕克说:“我们现在正处于一个高度敏感时期。无论你作出何种举动,都有人会从中读出某些信息。”<br />
<br />
　　美国微软―全国广播公司报道,美国政坛眼下处于敏感时期,保守派强烈反对奥巴马的政策,尤其是医疗改革方案。他们正抓紧一切机会揪出奥巴马的失误,做为“论据”支持自己的观点。<br />
<br />
　　早有先例<br />
<br />
　　奥巴马并非首次遭遇“鞠躬门”。他今年4月参加二十国集团伦敦金融峰会期间,曾因向沙特国王阿卜杜拉“鞠躬”遭非议。<br />
<br />
　　奥巴马当时弯着腰、上半身向前倾斜,向阿卜杜拉致意。保守派媒体《华盛顿时报》随后发表文章,指责奥巴马“贬低美国力量与尊严”,违反美国礼仪传统。<br />
<br />
　　同样来自民主党的美国前总统比尔・克林顿也有过同样遭遇。克林顿1994年因向明仁天皇“疑似鞠躬”挨批。《纽约时报》当时撰文写道:“白宫南草坪那一幕永远无法抹去:一个谄媚的总统和一个日本天皇。”<br />
