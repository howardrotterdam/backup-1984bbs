																											<div id="postmessage_173020" class="t_msgfont">LiPeng之女李小琳：能力之外的资本等于零 <br />
<img src="http://www.chinaelections.org/uploadfile/200907/20090721140313224.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
身为中国电力国际发展公司首席执行官，又是中国国家领导人LiPeng的女儿，李小琳走到哪里都备受瞩目，她开诚布公地表示：“能力之外的资本等于零。” <br />
<br />
　2009年5月底，“气候变化问题全球商业峰会”在丹麦首都哥本哈根举行，800多位全球工商界巨头出席了大会。联合国秘书长潘基文致了开幕词。在这些精英当中，有一位气质出众的中国女性格外引人注目。她，就是李小琳，中国电力投资集团副总经理、中国电力国际发展有限公司董事长。<br />
<br />
　　李小琳在中国电力市场被称为“一姐”，还曾荣登美国《财富》杂志全球商界女强人榜，被媒体称为“女电王”、“站在国际资本肩上的美女CEO”。记者眼前的她，温文尔雅、自信淡定。坦诚的交流中，让人感受到她话语背后的独特个性。<br />
<br />
　　李小琳之所以备受关注，还因为她有位当过中国国务院总理的父亲。媒体采访几乎都离不开这样的问题：你认为是什么原因让你坐到今天这个位置？这其中有家庭的因素吗？<br />
<br />
　　李小琳并不回避，坦率地回答：“我的成长是自己一步一步努力的成果。从大学毕业到去基层工作，从最初的技术员、工程师、科长、副处长、处长、副总裁、到总裁，我一个台阶也没有漏。一个人出生在比较好的家庭，如果没有自己的努力，只靠父辈的影响，即使给了你这个位置，你也是扶不起的阿斗！”<br />
<br />
　　李小琳很自信，她说对于自己而言：“能力之外的资本等于零”。<br />
<br />
　　父亲的“小跟班”<br />
<br />
　　在李小琳心目中，“前辈的荣誉和业绩永远属于前辈。它可以让你感到幸福和自豪，但不会成为靠山。”每每有人提及父亲LiPeng，李小琳的言语中总是充满尊敬，她眼中的父亲“意志坚定，生活有目标，做事认真，总是朝目标不断努力。”李小琳说，父母亲并没有刻意去教儿女应该怎样，但在家庭的熏陶下，他们都继承了父母的意志与品格。<br />
<br />
　　李小琳1961年出生于北京，她自称小时候是父亲的“跟班”，十几岁起就跟着父亲去很多电站考察。“当时中国较大的水电站、火电站我都看过，非常熟悉。”父亲头戴安全帽指挥工程技术人员施工的情景，李小琳至今历历在目。“小时候，看到父亲为中国的电力业四处奔跑，我从那时起就萌生了一个念头，长大后像父亲那样，头戴着安全帽下一线，指挥千军万马。”<br />
<br />
　　李小琳跟着父亲下基层访问时，LiPeng经常有意带她去当地的特困户家，指着在煤油灯下刻苦学习的孩子给女儿看，教育她要珍惜良好的学习环境，努力学习。<br />
<br />
　　在女儿心中，父亲LiPeng是一个非常爱学习的人，即便到了晚年退休以后，也没有停止学习。“父亲参加工作前，就开始每天坚持写日记。退休以后，他准备把这些日记分门别类，分10个不同的类别陆续出版，现在已经出版了好几本。”李小琳说，整理资料和写作，占去了LiPeng大量的退休时间。“有时我回家，看到父亲还在伏案工作，很辛苦。很多事情，他都喜欢亲力亲为，比如坚持自己上网、自己打字。他希望自己能够健康，能够看到未来改革开放的新成果。”<br />
<br />
　　身体好的时候，LiPeng还常常在家里看书，锻炼身体，尤其喜欢游泳。每每谈及父母和家庭的熏陶，李小琳脸上总是洋溢着幸福。在父亲的感染下，李小琳也一直坚持写日记。“我把每天点点滴滴的感悟、思考记录下来，把自己所做过的工作、在各种场合的发言和讲话，以及接受媒体访问的内容和各种来信，都一一收集起来。”李小琳曾辑录成多本自编的《心路历程》，“闲时经常翻阅这些东西，每次看都很有成就感，也能自省自励”。<br />
<br />
　　李小琳感到很幸运，她有一个和睦的家庭，“给人的感觉总是暖暖的”。上一辈和谐的婚姻，也影响着她的生活。“母亲(朱琳)有着远大的理想，她是哈尔滨军工大的高才生，曾做过电影演员，但她的理想就是像‘苏拉’一样给大多数人谋幸福。母亲是充满正义感的女性，非常率直、无私，我的直率、有爱心等好多秉性，就是受母亲的影响。”<br />
<br />
　　“母亲和父亲相互敬爱，几十年如一日。母亲每天会给父亲准备常规吃的药。他们一起吃饭，一起看电影，一起讨论事情。有争论的时候，爸爸让妈妈多一点，而妈妈承担家里的责任多一点。”这种家庭氛围延续到了李小琳的小家庭，她说：“我非常庆幸自己有一个和睦的家庭，有一个兄长般的先生和一个朋友般的女儿。”李小琳强调，自己是公众人物，她愿意承担这份责任，但不希望她的亲人承担这些，不希望社会关注他们，“我希望他们能过安静、健康、自然的生活”。<br />
<br />
　　一盏煤油灯与一辈子的梦想<br />
<br />
　　因为身上永远贴着“LiPeng之女”这样的标签，李小琳总被“高干子弟”，“大树底下好乘凉”这样的观念所笼罩。而一位了解李小琳的朋友告诉记者：“如果没有这个特殊的家庭背景，靠李小琳的才华和工作能力，说不定她能收获得更多。”<br />
<br />
　　李小琳听到这些议论，笑了笑，说：“的确，我成长的家庭大家都知道，每个人的成长其实都与家庭和社会分不开，家庭对于一个人很重要。我出生的这个家庭给我更多的是精神层面的影响。”李小琳向记者回顾了她的成长历程。<br />
<br />
　　“父亲的良苦用心没有白费，我从小学习就非常刻苦。”李小琳从少年时期就表现出良好的组织和管理能力，学习成绩优异，一直在学校担任班长、大队长、团支部书记等职务。“那时候父母工作忙，我13岁就开始当家里的账房先生，父母的工资都交到我手里，我来管一日三餐。”李小琳开心地回忆。<br />
<br />
　　可能连LiPeng也没有想到，他带着女儿看贫困孩子学习的情景，点燃了女儿的梦想。“那时候农村还没有电灯，用一个铁皮罐头壳和一绺延伸在外的棉线做成的煤油灯来照明。就是这盏破旧的煤油灯，让我产生一种强烈的使命感，长大以后要把光明撒播到每一个黑暗的角落。这也是我后来学习电力专业，从事电力事业的初衷。”<br />
<br />
　　1988年，李小琳获得清华大学电力系统自动化专业硕士学位。她在电力技术学院当过老师，在华北电力公司当过调度员、技术员、工程师，之后又调入了能源部。身为能源部国际司经贸处副处长，她有机会和世界各地的优秀管理者接触。“从一个单纯的技术型工程师转变成懂技术善管理的复合型人才，我发现自己急需‘充电’和知识更新。”第二年，李小琳有机会考入了联合国一个援助项目，去美国麻省理工大学斯隆商学院学习管理。据资料记载，斯隆商学院的名气和影响力一度超过了哈佛商学院，克林顿在担任美国总统期间所倚靠的经管智囊团成员，多半出自这里。“我倍加珍惜这次学习的机会，那两年成为我企业管理的新起点。”两年后，李小琳感叹自己如同脱胎换骨，后到电力部工作的她，做了经贸处处长。<br />
<br />
　　“海归”后不久，李小琳从一个文件中发现，境外有人想利用中国电力行业的概念做基金。这种在国外投资领域俗称“炒概念”的伎俩，被李小琳识破了。她马上给当时的电力部长呈报告：“国家发展需要电力先行，但尚缺资金，资金缺口至少2000个亿，建议在境外成立一个受法律保护的规范的公司，即成立‘正规军’，把国际上大的基金、财团吸引到我们这里，参与中国的电力建设。”李小琳的建议得到了电力部的肯定，她被抽调组建中国电力国际有限公司，并担任筹备组组长。<br />
<br />
　　回顾那段经历，李小琳坦言：“当时有独上高楼的茫然，你去做，却不知道怎么做，在未知中寻找出路。”她没有想到，这条路自己闯了整整10年，经历了“一波三折”。“一波”是在成立公司时，虽说建议被采纳了，但从和国家各个部委打交道，到最后确立去香港成立公司，差不多用了两年时间。而所谓“三折”，是之后找境外资金投入电力市场，中间有3次时机，却都因为观念、政策等因素一次次错过。第一个上市的时机是1997年。那年香港回归，香港股市上的中国概念红筹股很火。但资产“零利润”注入，却让上市成了没有依托的幻想。错失机会，李小琳立刻寻找第二次时机。她提出引进战略投资伙伴，然后再上市。从做好方案到报批，转眼到了1998年，一场金融风暴，又让上市泡汤了。李小琳说：“当时，我们的资产负债率已达到96%，这是个非常危险的财务状况。”她与同事们一起穿梭于香港各大银行之间，调整债务结构，化险为夷。眼看第三次机会到了，然而又因为企业根基不牢，还是没能上市。<br />
<br />
　　直到2004年10月15日，中电国际旗下中国电力终于在香港主板市场上市交易。那时，香港各大媒体刊登的一幅照片中，被昵称为“红衣美人”的李小琳正在接电话。接电话的那一刻，正是李小琳进入香港联交所的前一分钟。电话里说，李总，今天大市不好。李小琳镇定自若地回答，“基于我国的经济形势，基于我们电力的形势，基于我们母公司强有力的支持，我们一定会给股东一个满意的合理的回报！”说着，李小琳举起了手。香港报纸把她这个手势解读为“股市升”的好彩头。那天在大盘唱跌的情况下，中国电力升了17%。<br />
<br />
　　奶奶对全家影响深远<br />
<br />
　　10年中，李小琳在中电国际内部一直有很高的声望和影响力。“老总是个宽容的人，很讲究团队的和谐，其实是刚柔相济，尤其是遇到困难时，更能感觉到她的刚强和执着。”中电国际的一位员工这样评价李小琳。<br />
<br />
　　“一个企业的正常有效运转，有两条纽带发挥作用，一是产权制度纽带，另一个就是精神文化纽带”，李小琳说，世界著名的“长寿公司”都有一个共同特征，就是他们都有特色鲜明、积极向上的企业文化。<br />
<br />
　　李小琳提出“静水深流”的和谐管理理念。在她看来，水是天地万物之源，“水归于静、沉于思、流于恒。静水深流既是做企业的一种境界，也是为人的境界。人生就好比一个锯木的过程，没有慢慢锯的过程，就没有最后‘咔嚓’木断的一声。”她说自己也是一点一点锯过来的。<br />
<br />
　　李小琳说，“企”字无人则止，员工既是和谐企业的主体，又是“和谐”的创造者，只有企业与员工和谐了，其他方面的和谐，才有根基和依托。<br />
<br />
　　一次，一位朋友向李小琳诉苦：买了一堆名牌衣服，但穿出来总感觉不对，希望能给点建议。李小琳听了，微微一笑，告诉朋友：服装本身没有问题，关键在于搭配是否协调。于是，她与朋友交流了一些穿衣打扮的小窍门。朋友受到启发，穿着打扮的效果好了许多。<br />
<br />
　　李小琳说，企业的领导团队建设似乎也有这样的问题。“我们有许多优秀的管理人才，如果搭配得当，结构合理，各司其职，这股力量将是1+1〉2，反之，如果搭配不当，结构不合理，则可能出现1+1〈2，甚至1+1〈1。作为现代企业，我们需要优秀的个体，更需要集体的最佳结构组合，其中的关键就在于搭配的艺术。”<br />
<br />
　　在李小琳心目中，“好的企业文化就像一本‘真经’，能让你的团队有思想的认同感和责任感，能给工作以意义，给发展以动力。”<br />
<br />
　　其实，李小琳能够将中国传统文化用于企业管理，也源于家庭的熏陶。LiPeng的父亲李硕勋，是一名革命烈士，1931年8月因组织琼崖游击队军事会议而被国民党密探逮捕。在狱中，他给夫人赵君陶写下遗书，以平静的心态从容就义。他的夫人赵君陶，后成为我国著名的教育学家，博览群书，志趣高雅。<br />
<br />
　　至今，李小琳还记得奶奶坐在院中，用四川话给她讲故事的情景。“北京那小小的四合院，被奶奶装扮得永远芳草萋萋、枝繁叶茂。奶奶的故事特别多，我觉得很有趣，问她：‘您怎么知道这么多故事？’奶奶指着桌上的《资治通鉴》说，‘在这里面呀，你翻一翻。’”李小琳趴在厚厚的《资治通鉴》上，又不认识那么多字，奶奶便在桌上放一本《康熙大字典》，教她查字典。李小琳说：“奶奶是一个极富文学素养和文化底蕴的女性，她在我心灵中播下了中华优秀文化的种子”。<br />
<br />
　　在奶奶的影响下，李小琳从小就爱读古书，包括中医方面的书籍，谈起《易经》、《黄帝内经》等典籍，她娓娓道来，兴致颇浓。“从《诗经》、《楚辞》到唐诗、宋词、《资治通鉴》、《古文观止》，我都有所研读。直到今天，《桃花源记》、《岳阳楼记》等许多名篇，我还能够背诵。”<br />
<br />
　　赵君陶晚年喜好书法，写得一手隽永娟秀的“赵”体字，气韵生动、形神兼备。受奶奶的影响，李小琳一直偏爱繁体字。翻看李小琳的书法，架构清奇，娟秀中透出一股英气，这也是奶奶留给她的最珍贵遗产。<br />
<br />
　　李小琳说：“爷爷28岁慷慨就义前，给奶奶留下家书，嘱托她好好抚养子女，把他们培育成才。奶奶果真没有辜负爷爷的重望，如今不仅儿孙满堂，并且从爸爸到我们，个个都有所作为。”<br />
<br />
　　“女强人首先是女人”<br />
<br />
　　李小琳是个懂得内外兼修的知性女性。对于人们将她看成是标准的“女强人”，李小琳说：“‘女强人’首先是‘女人’。是女人，就要重视女性美。”<br />
<br />
　　之前，记者曾听很多人描述过李小琳，年轻、漂亮、爱美，从首饰、服装到手袋，都十分考究。李小琳笑着解释她对“美丽”的理解：“我觉得美丽应该是美丽加能力，也就是‘美力’。女性能否拥有‘美力’，最终还要依靠自己。其实我的衣服没有什么名牌。我平时工作很忙，经常在开会的时候看到住的酒店里有合适的衣服，就买下来。说到化妆，我平时基本上都会化妆。保养其实没有什么秘诀，我相信‘相由心生’。”<br />
<br />
　　李小琳对女强人的“强”，也有自己的理解，她说那不只是刚强之“强”，还应该是柔韧之“强”。“正如水的屈伸、水的刚柔之道，百折不挠。人的一生当中，没有谁能保证一帆风顺，都可能起起伏伏，我们要学会微笑地去接受每一件事情。我一直认为女性在尊重人、理解人、宽容人、沟通与悟性几个方面有独特优势，韩国女子大学就有‘用温柔改变世界’的信条。而这些，恰恰是一切优秀的管理者不可或缺的素质。”<br />
<br />
　　李小琳有个业余爱好是登山，她说，做工作、干事业也像是登山，不过那是无形之山，征服它们需要更大的勇气和毅力，远比登自然之山艰难得多。<br />
<br />
　　“跋山涉水行大道，精进深耕不辞劳。俯首甘为孺子牛，扭转乾坤数英豪。”这是采访结束后，李小琳送给记者的一首小诗。记者知道，她是属牛的，2009年恰好是牛年。牛的忠诚、踏实、勤劳、奉献精神，正是李小琳所崇尚的。这位女性正以牛的孺子精神鞭策、激励自己，以新的姿态昂然奋进在人生路上……&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; <br />
 <br />
