9. Beyond Good and Evil<br />
<br />
<br />
Author: Freidrich Nietzsche<br />
Publication date: 1886<br />
Score: 28<br />
Summary: An oft-scribbled bit of college-campus graffiti says: “‘God is dead’--Nietzsche” followed by “‘Nietzsche is dead’--God.” Nietzsche’s profession that “God is dead” appeared in his 1882 book, The Gay Science, but under-girded the basic theme of Beyond Good and Evil, which was published four years later. Here Nietzsche argued that men are driven by an amoral “Will to Power,” and that superior men will sweep aside religiously inspired moral rules, which he deemed as artificial as any other moral rules, to craft whatever rules would help them dominate the world around them. “Life itself is essentially appropriation, injury, overpowering of the strange and weaker, suppression, severity, imposition of one’s own forms, incorporation and, at the least and mildest, exploitation,” he wrote. The Nazis loved Nietzsche.<br />
