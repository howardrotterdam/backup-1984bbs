8. The Course of Positive Philosophy<br />
<br />
<br />
Author: Auguste Comte<br />
Publication date: 1830-1842<br />
Score: 28<br />
Summary: Comte, the product of a royalist Catholic family that survived the French Revolution, turned his back on his political and cultural heritage, announcing as a teenager, “I have naturally ceased to believe in God.” Later, in the six volumes of The Course of Positive Philosophy, he coined the term “sociology.” He did so while theorizing that the human mind had developed beyond “theology” (a belief that there is a God who governs the universe), through “metaphysics” (in this case defined as the French revolutionaries’ reliance on abstract assertions of “rights” without a God), to “positivism,” in which man alone, through scientific observation, could determine the way things ought to be.<br />
