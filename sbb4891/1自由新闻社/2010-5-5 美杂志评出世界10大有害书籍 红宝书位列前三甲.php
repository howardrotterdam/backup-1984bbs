7. The Feminine Mystique<br />
<br />
<br />
Author: Betty Friedan<br />
Publication date: 1963<br />
Score: 30<br />
Summary: In The Feminine Mystique, Betty Friedan, born in 1921, disparaged traditional stay-at-home motherhood as life in “a comfortable concentration camp”--a role that degraded women and denied them true fulfillment in life. She later became founding president of the National Organization for Women. Her original vocation, tellingly, was not stay-at-home motherhood but left-wing journalism. As David Horowitz wrote in a review for Salon.com of Betty Friedan and the Making of the Feminine Mystique by Daniel Horowitz (no relation to David): The author documents that “Friedan was from her college days, and until her mid-30s, a Stalinist Marxist, the political intimate of the leaders of America’s Cold War fifth column and for a time even the lover of a young Communist physicist working on atomic bomb projects in Berkeley’s radiation lab with J. Robert Oppenheimer.”<br />
