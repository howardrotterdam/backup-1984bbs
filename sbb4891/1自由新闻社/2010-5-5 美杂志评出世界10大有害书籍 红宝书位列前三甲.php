<br />
<br />
Author: Karl Marx<br />
Publication date: 1867-1894<br />
Score: 31 <br />
Summary: Marx died after publishing a first volume of this massive book, after which his benefactor Engels edited and published two additional volumes that Marx had drafted. Das Kapital forces the round peg of capitalism into the square hole of Marx’s materialistic theory of history, portraying capitalism as an ugly phase in the development of human society in which capitalists inevitably and amorally exploit labor by paying the cheapest possible wages to earn the greatest possible profits. Marx theorized that the inevitable eventual outcome would be global proletarian revolution. He could not have predicted 21st Century America: a free, affluent society based on capitalism and representative government that people the world over envy and seek to emulate.<br />
