Ten Most Harmful Books of the 19th and 20th Centuries<br />
05/31/2005<br />
<br />
<br />
HUMAN EVENTS asked a panel of 15 conservative scholars and public policy leaders to help us compile a list of the Ten Most Harmful Books of the 19th and 20th Centuries. Each panelist nominated a number of titles and then voted on a ballot including all books nominated. A title received a score of 10 points for being listed No. 1 by one of our panelists, 9 points for being listed No. 2, etc. Appropriately, The Communist Manifesto, by Karl Marx and Friedrich Engels, earned the highest aggregate score and the No. 1 listing.<br />
<br />
1. The Communist Manifesto<br />
<br />
<br />
Authors: Karl Marx and Freidrich Engels<br />
Publication date: 1848<br />
Score: 74<br />
Summary: Marx and Engels, born in Germany in 1818 and 1820, respectively, were the intellectual godfathers of communism. Engels was the original limousine leftist: A wealthy textile heir, he financed Marx for much of his life. In 1848, the two co-authored The Communist Manifesto as a platform for a group they belonged to called the Communist League. The Manifesto envisions history as a class struggle between oppressed workers and oppressive owners, calling for a workers’ revolution so property, family and nation-states can be abolished and a proletarian Utopia established. The Evil Empire of the Soviet Union put the Manifesto into practice.<br />
<br />
2. Mein Kampf<br />
<br />
<br />
<br />
Author: Adolf Hitler<br />
Publication date: 1925-26<br />
Score: 41<br />
Summary: Mein Kampf (My Struggle) was initially published in two parts in 1925 and 1926 after Hitler was imprisoned for leading Nazi Brown Shirts in the so-called “Beer Hall Putsch” that tried to overthrow the Bavarian government. Here Hitler explained his racist, anti-Semitic vision for Germany, laying out a Nazi program pointing directly to World War II and the Holocaust. He envisioned the mass murder of Jews, and a war against France to precede a war against Russia to carve out “lebensraum” (“living room”) for Germans in Eastern Europe. The book was originally ignored. But not after Hitler rose to power. According to the Simon Wiesenthal Center, there were 10 million copies in circulation by 1945.<br />
<br />
3. Quotations from Chairman Mao<br />
<br />
<br />
Author: Mao Zedong<br />
Publication date: 1966<br />
Score: 38<br />
Summary: Mao, who died in 1976, was the leader of the Red Army in the fight for control of China against the anti-Communist forces of Chiang Kai-shek before, during and after World War II. Victorious, in 1949, he founded the People’s Republic of China, enslaving the world’s most populous nation in communism. In 1966, he published Quotations from Chairman Mao Zedong, otherwise known as The Little Red Book, as a tool in the “Cultural Revolution” he launched to push the Chinese Communist Party and Chinese society back in his ideological direction. Aided by compulsory distribution in China, billions were printed. Western leftists were enamored with its Marxist anti-Americanism. “It is the task of the people of the whole world to put an end to the aggression and oppression perpetrated by imperialism, and chiefly by U.S. imperialism,” wrote Mao.<br />
<br />
4. The Kinsey Report<br />
<br />
<br />
Author: Alfred Kinsey<br />
Publication date: 1948<br />
Score: 37<br />
Summary: Alfred Kinsey was a zoologist at Indiana University who, in 1948, published a study called Sexual Behavior in the Human Male, commonly known as The Kinsey Report. Five years later, he published Sexual Behavior in the Human Female. The reports were designed to give a scientific gloss to the normalization of promiscuity and deviancy. “Kinsey’s initial report, released in 1948 . . . stunned the nation by saying that American men were so sexually wild that 95% of them could be accused of some kind of sexual offense under 1940s laws,” the Washington Times reported last year when a movie on Kinsey was released. “The report included reports of sexual activity by boys--even babies--and said that 37% of adult males had had at least one homosexual experience. . . . The 1953 book also included reports of sexual activity involving girls younger than age 4, and suggested that sex between adults and children could be beneficial.”<br />
<br />
5. Democracy and Education<br />
<br />
<br />
Author: John Dewey<br />
Publication date: 1916<br />
Score: 36<br />
Summary: John Dewey, who lived from 1859 until 1952, was a “progressive” philosopher and leading advocate for secular humanism in American life, who taught at the University of Chicago and at Columbia. He signed the Humanist Manifesto and rejected traditional religion and moral absolutes. In Democracy and Education, in pompous and opaque prose, he disparaged schooling that focused on traditional character development and endowing children with hard knowledge, and encouraged the teaching of thinking “skills” instead. His views had great influence on the direction of American education--particularly in public schools--and helped nurture the Clinton generation.<br />
