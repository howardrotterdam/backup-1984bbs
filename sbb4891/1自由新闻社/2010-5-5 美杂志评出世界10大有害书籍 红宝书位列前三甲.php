																											<div id="postmessage_599157" class="t_msgfont"><a href="http://www.humanevents.com/article.php?id=7591" target="_blank">http://www.humanevents.com/article.php?id=7591</a><br />
<br />
美国HUMAN EVENTS周报找了15个保守学者评出来的，200年以来的10大有害书籍。马克思和恩格斯合著的《共产党宣言》，众望所归荣膺榜首。<br />
<br />
1、《共产党宣言》 作者：马克思&amp;恩格斯，1848年<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;此二人身为共产主义教父，前者一生的大部分生活都是后者资助的。本书号召工人革命，消灭财产、家庭和国家，建立无产阶级乌托邦。该理论付诸实践后，产生了邪恶帝国苏联。<br />
<br />
 <br />
<br />
2、《我的奋斗》作者：阿道夫・希特勒，1925-1926年<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;希特勒因领导“啤酒馆暴动”而被投入监狱，他在狱中出版了该书。书中阐述了他的种族主义、反犹主义以及“生存空间”理论，直接导致了二战和犹太大屠杀。1945年二战结束时，该书的发行量已经超过了1000万册。<br />
<br />
 <br />
<br />
3、《毛主席语录》作者：毛主席，1966年<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;1976年去世的毛，在二战结束后领导红军战胜了蒋介石的国民党，夺得中国的控制权。1949年，他建立了中华人民共和国，用共产主义奴役世界上人口最多的国家。1966年，《毛主席语录》出版，又称红宝书，是他发动文化大革命的工具之一。（后面引用了一句语录，我没有读过，不敢乱翻）。<br />
<br />
 <br />
<br />
4、《金赛报告》作者：阿尔弗雷德・金赛 ，1948年<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 印第安那大学动物学家金赛，1948年出版了《男性性行为》，被称为《金赛报告》。5年后，他又出版了《女性性行为》。去年，电影《金赛》上映时，华盛顿时报称，他的报告震惊了美国，他指出按照40年代的法律，95%的美国男人都可被指控性侵犯。……该书甚至认为，成年人和孩子之间的性行为是有益的。<br />
<br />
 <br />
<br />
5、《民主主义与教育》作者：杜威，1916年<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;进步主义哲学家约翰・杜威曾任教于芝加哥大学和哥伦比亚大学，签署过《人道主义宣言》，拒绝传统宗教和道德绝对主义。他在该书中，诋毁旨在培养传统品质的学校教育，主张以思考代替努力学习。他的观点后来极大影响了美国的公共教育，直接孕育了“克林顿一代”。<br />
<br />
 <br />
<br />
6、《资本论》作者：马克思，1867-1894年<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;马克思生前出版了该大部头书的第一卷，完成的后面两卷手稿后来由其终身资助人恩格斯出版。该书把资本主义描绘成人类社会发展的一个丑陋阶段，资本家毫不留情地压榨剩余价值。马克思的理论的最终结果是全球无产者大革命。他没能预见21世纪的美国：自由、丰裕，以资本主义和代议制政府为基础，人人都羡慕并拼命仿效。<br />
<br />
 <br />
<br />
7、《女性的奥秘》 作者：贝蒂・弗里丹，1963年<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 这本书没听说过，不翻了。<br />
<br />
 <br />
<br />
8、《实证主义哲学》作者：孔德，1830-1842年<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;社会学奠基人奥古斯特・孔德，生于法国天主教保皇党家庭，在青年时期宣布放弃信仰。后来，他在六卷本《实证主义哲学》中首次用了“社会学”这个词。他创立的理论认为，人类意识已经超越“神学，通过“形而上学”，达到了“实证主义”的境界。<br />
<br />
 <br />
<br />
9、《善恶之彼岸》作者：尼采，1886年<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;尼采这本书的主题即是那句最著名的话：“上帝死了”。尼采写道：“生命的本质就是占有、伤害，压倒陌生人和弱者，压迫、惩罚、被迫自我接受，兼并，以及哪怕是最少程度、最温和的攫取。” 纳粹爱尼采。 <br />
