<br />
因“没有做志愿者和教师的资格”，也不想被媒体打扰，卢安克关闭了自己的博客。<br />
<br />
年初，中央电视台播出了对德国籍志愿者卢安克的专访，并感动了众多普通的中国人。但同时给他带来的，还有一个发自广西自治区公安厅的警告：即他没有在中国做志愿者和教师的资格。――言下之意，就是让他不要在媒体上出现、也不要再谈论有关中国教育和留守儿童的话题了。否则“有关部门”将会追究他的“不法行为”，并随时有可能把他驱逐出去。<br />
<br />
这太不合情理了。如果广西公安厅“有关部门”看过有关卢安克的报道，并到他作为志愿者、义务服务了近十年之久的广西河池市东兰县切学乡板烈村小学了解一下情况，就会尽快为之提供便利――如果卢安克从某种条文上讲，确实没有充当志愿者和乡村小学教师的“资格”。但广西公安厅不仅不提供帮助和服务，反而对之发出了警告：这就等于说，一个热爱中国、并为中国乡村教育的实践和研究付出了自己全部心血和生命的外国青年，是不受中国政府欢迎的；一个有着与白求恩和雷锋类似行为的人，在中国是“非法的”。<br />
<br />
这样的事，已不是第一次发生了。1997年10月，在广西南宁残疾人职业学校义务授课的卢安克，就被南宁市公安局扣留过护照，并罚款3000元。――要知道，卢安克在广西义务执教十几年，从未收受过学校一分钱工资。他每年三四千元的生活费均由其远在德国汉堡的父母提供，月支出不超过200元，还要节省下一部分来印制有关的教学材料，或帮助家境贫寒的学生。<br />
<br />
1999年，回到中国的卢安克设立了一个办事处，以使自己服务的身份和目的合法化。此后，为了避开应试教育只重考试成绩的弊端，更好地实践和探寻素质教育的方法，他又从南宁辗转到了阳朔，最终选定了全国特困县广西东兰，并落脚在该县一个名叫板烈的偏僻村庄里。在板烈小学，他吃着只搁点盐炒的红薯叶和莲花白，度过了十年的乡村教师生活，也不领一分钱工资。其间，这位与中国最贫困地区农民一道承受着最艰苦的物质生活，同时进行着极富创造力实践与研究的青年，还得过乙型肝炎，并遭遇车祸险些失去了性命。<br />
<br />
2006年，签证到期的卢安克不得不又一次离开中国，这个他已经交付出了自己命运的国度。加入中国籍的愿望不过是幻想，他的实践、研究成果并未得到出版界的认可，尽管有新闻界的友人倾力相助，教育专家、名人学者仍不置一辞；而一个没有做出过“重大贡献”的外国人，哪怕他是白求恩，或者“洋雷锋”，也是不可能获准加入中国国籍的。 <br />
2007年，执著的卢安克又回到了板烈，继续以志愿者身份做他的乡村教师，直到现在。从2001年起，卢安克开设了自己的网站和博客：网站中有他翻译的上百万字的教育论著；在博客中，他还无私地把自己所有成熟的研究成果和作品，都张贴了出来，力求对别人有所帮助。年初，当一直躲避媒体的他再次审慎地接受采访，想满足更多人的需要时，得到的，却是来自中国地方公安部门的警告。这也是今年四月，我在板烈见到卢安克时，他谢绝我采访的原因。卢安克所做的一切光明正大，他决不会在“非法”的阴影中做任何事情。<br />
 <br />
卢安克在关闭博客的说明中，让“本国人”不要为他感到“难受”：一方面是那种为别人的善行“难受”，而心怀叵测给人家“警告”的人；另一方面，是那些真心敬佩他、崇拜他的普通人。我不为卢安克难受，他付出一切追求的是自己的理想，服务的是“人类的精神”，受惠的是板烈村小学的孩子们。我难受的，是这个国家终于伤害到了百般隐忍的他。作为本国人，我必须表明自己的态度。</div>

							
							
							
							
