<br />
English.news.cn&nbsp; &nbsp;2010-05-31 02:13:25FeedbackPrintRSS<br />
<br />
NANJING, May 31 (Xinhua) -- Zhu Yongxiang, a 72-year-old Chinese communist, pondered and cast his ballot to vote for the village communist party chief in the first direct election held in his village Houcong earlier this month.<br />
&quot;This is real democracy since party members' votes decide who the winner is instead of nominations and appointments,&quot; said Zhu, a villager with 50 years of membership in the Chinese Communist Party (CPC).<br />
He gave his support to Chen Jiagui in the election for party secretary in Houcong Village in Nanjing, capital of east China's Jiangsu Province.<br />
Chen beat his rival 121 to 32 votes in the election observed by 200 party members, some of whom came to the event with mud still on their shoes, fresh from work in the fields.<br />
From April to June, similar direct elections were run in 806 villages in Nanjing, where CPC committees in 363 neighborhoods in urban areas have piloted general elections for grassroots leaders since last year.<br />
&quot;The CPC has been the ruling party for over 60 years, since New China was founded in 1949. However, its long-standing party cadre selection model has caused some social conflicts and tension between the public and grassroots CPC cadres,&quot; said Professor Shao Jianguo with the Party School of the Nanjing Municipal CPC Committee.<br />
He pointed out in the selection method, appointed officials get the &quot;power&quot; from the the higher-ups rather, and consequently they are likely to care more about their superiors than the masses.<br />
With the ongoing direct elections in villages, Nanjing has become the only Chinese city to have extended the direct elections of grassroots CPC cadres to both urban and rural areas on a large scale, said the professor.<br />
The CPC chose the city to pilot the first direct election in one neighborhood in 2004, a move considered a significant step toward boosting democracy within the Party, said Cai Xia, a professor with the Party School of the CPC Central Committee, adding that it is expected to push forward democracy in Chinese society,<br />
&quot;Intra-party democratic reform is crucial to the Party's foundation, since it brings changes to the kind of people that become CPC cadres,&quot; said Shao.<br />
Newly-elected party chiefs in the 806 villages with a population of 2.7 million are expected to take office in June.<br />
One of them, Chen Jiagui, the new party chief in Houcong Village, distinguished himself from four other candidates, who included township officials and college graduates.<br />
Actually, Chen had worked as the appointed party chief in the village for the past 10 years. He was thrilled about winning the post in the election.<br />
&quot;This time, the victory was hard won. I feel the weight of each ballot in my favor,&quot; he said.<br />
Villagers said Chen, a successful seedling tree grower himself, had led them to develop the tree plantation, which has become the backbone economy for boosting farmers' income.<br />
&quot;The party secretaries elected last year in urban communities have done a good job in helping residents resolve problems in daily life, like repairing street lights and roads,&quot; said Wang Qi, director of the organization department of Nanjing municipal CPC committee.<br />
He said Party members hope the village elections help boost the rural economy and raise farmers' income.<br />
Wang said the Nanjing committee has drafted election procedures based on experiences from the elections.<br />
&quot;The elections must abide by rules. There are procedures for electioneering, for example, and qualification examinations for candidates,&quot; he said.</div>

							
							
							
							
