																											<div id="postmessage_697690" class="t_msgfont"><div class="quote"><h5>引用:</h5><blockquote>原帖由 <i>那个谁</i> 于 2010-6-22 12:49 发表 <a href="https://1984bbs.com/redirect.php?goto=findpost&amp;pid=697337&amp;ptid=50361" target="_blank"><img src="https://1984bbs.com/images/common/back.gif" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /></a><br />
这样一条消息居然是英文的。 </blockquote></div>公推直选覆盖南京城市农村 为大范围民主提供思路<br />
2010年06月10日 11:32　来源：中国新闻网　参与互动(8)　　【字体：↑大 ↓小】<br />
<br />
　　(声明：刊用《中国新闻周刊》稿件务经书面授权) <br />
<br />
　　“公推直选”是进入新世纪以来中共党内民主具有标志性的创新成果之一。从最初四川平昌实行“公推直选”，首次打破党内“任命制”，至今，中共党内的民主试验已近10年。<br />
<br />
　　正如萨缪尔・亨廷顿所说，政治秩序或政治稳定取决于政治制度化水平和政治参与程度之间的关系。<br />
<br />
　　近10年来，先是各地单一试验，再是中共的十七大将“公推直选”作为党的民主政治建设的努力方向进行了充分肯定。如今，仅南京一地，已经有806个村党组织和363个城市社区党组织，在换届时全部实行公推直选。 <br />
<br />
<br />
<br />
<br />
　　南京，这个长江下游最重要的城市之一，基层党组织已经被民主“全覆盖”，民主在这个758万人的都市正待演进成蔚然大势。与以往基于一点的乡村党内民主的试验不同，南京的试验将为中国的民主演进提供一个崭新的城市样本。这个城市的努力，或将为中国更大范围的民主推进提供思路。<br />
<br />
　　南京：中共党内民主尝新<br />
<br />
　　在南京，把“公推直选”全面覆盖到城市农村，这在全国尚属首例。与以往来自于“点”上的民主试验不同，这一次由点及面，“全覆盖”意义在于，或将为更大范围的民主提供思路<br />
<br />
　　本刊记者/刘炎迅 (发自南京)<br />
<br />
　　桌子上，放着一摞书，都很厚，书名围绕着几个关键词，比如基层民主，乡村治理等等。<br />
<br />
　　杨花伟坐在旁边，修改一份有关“公推直选”的材料。他是中共南京市组织部研究室主任，最近这些日子，他的工作重心就在于此。“公推直选”是一件大事。今年，南京市的所有806个农村(涉农社区)都将以此方式换届选举出新的村支部书记和班子成员。<br />
<br />
　　“我们最近很忙的。”南京市雨花台区组织部里的几名年轻人异口同声。他们是这座省会城市党委组织系统里的一线人员，眼下这场全面展开的“公推直选”，不但考验着他们的工作能力，也考量着他们的体力。<br />
<br />
　　雨花台区委组织部长孙朝晖对《中国新闻周刊》说，4年前，他有过一次类似的考验，当年，他还是高淳县组织部长，那是个远离南京市主城区的下辖县，作为中组部确定的试点，让全县所有8个乡镇的党委书记和班子成员以“公推直选”的方式，先期完成党内权力交替。<br />
<br />
　　“当时写材料搞总结经常熬夜。”孙朝晖说，甚至有一次，因为汇报材料在最后一刻被全盘推翻，他不得不亲自上阵，连夜重写，“上级的意思很明确，直接一点，写问题，写不足。”<br />
<br />
　　在过去的几年里，南京一直推进基层党内民主的试点，每一次实践后，都是“写问题，写不足”，期望总结出一套成熟的操作经验。<br />
<br />
　　从点到面，南京市的基层党内民主不断推进，去年底实现城市社区党组织全面覆盖了“公推直选”，而今年则让“全覆盖”移向了整个城市的农村。南京，这个基层已经被民主“全覆盖”的城市，为中国的民主演进提供一个研究的样本。<br />
<br />
　　让能者掌权<br />
<br />
　　南京城滨江而居，江北有六合、浦口两区，江南则是经济和政治中心，有玄武、秦淮、建业、鼓楼、栖霞、雨花台、江宁、溧水和高淳等9个区县。<br />
<br />
　　在南京市中共党务系统提供给《中国新闻周刊》的说明中写道：“从实际情况看，南京市村级集体经济发展不平衡，村党委书记的致富能力还有待进一步增强。”<br />
<br />
　　从今年4月开始，这11个区县内，所有806个村庄(涉农社区)，面临中共党组织领导班子换届。<br />
<br />
　　这成为一个契机。<br />
<br />
　　在南京市官方的说法里，“这次换届的重中之重，就是要通过民主、公开、竞争、择优的方式，选出一支发展能力强、带富能力强、热心为农民群众服务的村书记队伍。”<br />
<br />
　　竹镇大泉村是“农民的好支书―李元龙”的故乡，是南京市公推直选的重点试点村。<br />
<br />
　　5月19日，上午8点半，大泉村党总支领导班子公推大会正式举行。来自全村的200多位中共党员和村民代表，为选出自己心目中李元龙式的“当家人”，投出一票。上世纪90年代，贫困的大泉村因为有了“农民的好支书”李元龙，而迅速改变了落后的面貌。他带领全村人新建了贯穿大泉村南北的五公里长水泥路；他说服每户村民，将1600亩低产田改造成良田&#8943;&#8943;<br />
<br />
　　公推直选共有9项程序，其中重要环节就是公推大会，也是最后出结果的一个环节。<br />
<br />
　　大会上，除了进行演讲陈述竞职外，村民们还能通过提问环节向每位竞职党员直接发问，进行零距离接触。<br />
<br />
　　村里的党员最简单的想法是，通过公开、民主的公推大会，用手中的选票，选出一位好支书，让村子更加富裕些。<br />
<br />
　　“21世纪什么最贵？人才。”南京市组织部一位不愿具名的官员告诉《中国新闻周刊》，这其实就是一次选拔人才的尝试，“提升选人用人公信度，为推动南京郊县农村经济发展奠定扎实的组织基础”，不必过分拔高和渲染。<br />
<br />
　　选举大会上，设立了秘密写票处，保证党员群众在无干扰的情况下自主表达意愿，选出真正有能力的人。<br />
<br />
　　在接受《中国新闻周刊》采访时，南京市委组织说，今年之举，是想进一步打通人才向农村基层一线输送的渠道，鼓励农村基层、有志于农村工作的区县和镇街机关年轻干部到农村去，鼓励优秀大学生村官、双带标兵、致富能手、农民经纪人、农民专业合作组织负责人，非公企业管理人员等参与竞争。<br />
<br />
　　官方期待，一支以45岁左右为主体，文化程度较高，熟悉农村工作，善于做群众工作的优秀村支书队伍能迅速形成。<br />
<br />
　　这么一来，村党组织班子结构的优化和整体功能的增强将得以实现，“人才到基层锻炼、干部从一线选拔”的培养选拔链也能最终形成。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;随着中共十七大明确了“逐步扩大基层党组织领导班子成员公推直选范围”，南京市此举的期望是，将公推直选制度化，常态化。<br />
<br />
　　在当地，公推直选要过九道关，经过广泛的报名后，参选人将驻点调研，参加公推大会，接受党员和群众代表的提问和辩论，然后是党工委票决，最后是选举大会。<br />
<br />
　　南京市全市共有11267人报名，有10570人通过资格审查，其中，党组织占19.1%，党员群众联名举荐2854人，占27%，自荐5697人，达到了53.9%。<br />
<br />
　　这里面，有现任村干部，有机关年轻干部，有大学生村官，有致富能手，有“双带”标兵，有复员退伍军人，还有外出务工返乡党员，以及非公企业党员业主。 <br />
<br />
<br />
<br />
<br />
　　8万余名党员、近270万群众共同完成了这次规模庞大的直选，至此，南京成为全国首个在村和社区基层中共党组织全面实现公推直选的城市。<br />
<br />
　　民主新演进<br />
<br />
　　与去年“全覆盖”的社区选举不同，面对农村党组织的“全覆盖”公推直选，南京市组织部显得比较低调。<br />
<br />
　　去年8月底，南京的363个城市社区全部采用公推直选这种方式选出党组织书记、副书记和委员。当时此举得到了中组部的肯定。在中组部举办的全国组织部长培训班上，南京市作了经验介绍。<br />
<br />
　　时值十七届四中全会召开之际，中组部派专员前来调研，听取经验，这也为后来全会通过的决议提供了某种实践层面的素材。<br />
<br />
　　在去年的社区选举成功后，南京市还组织了专家研讨会。这些动作都表明，南京市在实践之后期望从学理和制度层面总结经验。<br />
<br />
　　这也是一以贯之的传统，演进路线清晰明了。官方背景的《望》杂志曾介绍，公推直选是高层指导与基层探索良性互动的成功范例。其中江苏省及南京市的探索热情强化了这个互动，也加快了高层决策的进程。<br />
<br />
　　早在2004年，江苏省宿迁市搞试点，在宿豫区黄墩镇举行了党员大会，直接选举了镇党委书记。<br />
<br />
　　“黄墩直选”是江苏省探索公推直选的第一个尝试，得到高层肯定，这坚定了江苏省探索公推直选的信心。《望》杂志披露，“有关部门就落实中央领导的批示而专门下发了文件，要求江苏省委在稳妥的基础上继续扩大公推直选试点，用实践回答中央领导关心的问题。”随后在泗洪县、溧阳市、仪征市等部分乡镇党组织换届中进行了实施。<br />
<br />
　　现任南京市委组织部部长、时任江苏省委组织部副部长王奇就参与了“黄墩直选”运作，南京市本次能够在基层党组织换届中全面运用公推直选，与王奇的坚持分不开。<br />
<br />
　　2004年12月，南京市首次在白下区淮海路街道淮海路社区进行公推直选党组织领导班子的试点。<br />
<br />
　　2005年4月至5月，另有五个社区也紧随其后，被列为公推直选社区党组织班子试点。<br />
<br />
　　同年底，在江宁区的土桥镇镇党委领导班子列为试点。<br />
<br />
　　到了2006年，高淳县8个乡镇的试点全面展开也就顺理成章。时任高淳县组织部长的孙朝晖告诉《中国新闻周刊》，“高淳的那个试点是中组部指定的，那时候李源潮部长还在江苏当省委书记，他到中组部去汇报，然后中组部就指定在江苏两个县进行试点，李书记就定下来苏南和苏北各选一个点，进行试点，苏南选了高淳，苏北选了仪征。”<br />
<br />
　　孙朝晖坦言，那一年高淳试点，已是当时规模最大的试点，“一开始做的方案还不是书记直选，因为这个跟当时的党章规定有冲突，第一轮方案做完以后，李源潮看完，觉得探索的步子太小了，又到中组部建言，试点可以适当地有些探索性，后来中组部特批，同意进行镇党委书记的直选。”<br />
<br />
　　雨花台区组织部长孙朝晖说，南京这些年在基层民主上的不断提速，有几个原因，比如南京市每年都出一些政治建设的目标任务，去做一些这方面的事情，主动扮演探索者的角色。而南京通过这几年的探索，本身也从中获益了。<br />
<br />
　　“所以这就形成了一种良性互动，逐步深入。”孙朝晖举例，比如在2006年，高淳乡镇工作的公推直选试点完成后，紧跟着就是工会、共青团领导的换届，他们都主动提出来，也用公投直选的办法，进一步扩大民主来选领导班子。<br />
<br />
　　事实上，2006年高淳试点全面成功后，南京市官方已经总结出一套公推直选的方案，他们在等待时机。<br />
<br />
　　2009年，契机到了。<br />
<br />
　　这个城市先是尝试着在一些机关事业单位尝试公推直选。接着，这一年恰逢南京市所有城市社区党组织换届，于是南京市决定，率先在城市社区党组织班子换届中，运用公推直选产生了363个新的社区党组织班子。<br />
<br />
　　而在这期间，中共江苏省委组织部也派出了几路人马对十七大前采用公推直选换届的73个乡镇进行回访性调查。<br />
<br />
　　据参与调查的人员曾向《望》杂志透露，“此举旨在全面评估公推直选的运用效果以及完善公推直选的路径”。<br />
<br />
　　今年，随着806个行政村的党组织班子“公推直选”换届完成，接下来，这个城市还将忙于村委会的换届选举。<br />
<br />
　　新的选人机制，将造就新的基层党委班子行事风格。这些新晋的党的基层干部，如何与村民委员会良好的互动，也成为各界关注的焦点之一。<br />
<br />
　　在接下来的村委会公推直选中，南京市有关方面坦言，有些担心，如果村委会主任竞选不上党委书记，岂不是很尴尬，至今的结果还是让人放心，没有出现这样的局面。<br />
<br />
　　当地官员也流露出担心，原来村民委员会主任由村民自治选出，在党组织书记由上级委任的时代里，这很容易解决，上级只需要任命这个民选的主任为党组织书记，但如今在南京，基层民主“全面覆盖”后，有可能将来出现党员直选出的书记与民选出的村委主任不是一个人，产生冲突。党的民主与人民民主，都是民主产生的，到底谁说了算，这事实上增加了广泛存在的两委之间纠结的可能。<br />
<br />
　　如果党组织书记和村委会主任分属两人，理论上也不会有问题，“按照法规，也是党组织书记一把手。”但在现实中，书记和主任不同的个性特点都会影响整个权力的正常运作。<br />
<br />
　　中共南京市委期望的是，引导和衔接这两委的换届选举，加大符合条件的村党组织书记通过法定程序兼任村委会主任的工作力度，统筹选配好村“两委”班子，实现“党内民主引领人民民主”。 ★</div>

							
							
							
							
