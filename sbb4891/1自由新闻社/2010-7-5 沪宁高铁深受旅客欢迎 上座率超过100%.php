7月11日起的沪宁高铁票暂停销售<br />
<br />
稿件来源：新闻晨报&nbsp; &nbsp;晨报记者 宋杰<br />
<br />
&nbsp; &nbsp; 上海暂停发售7月11日及以后沪宁城际高铁车票。记者昨天从铁路部门获悉，暂停预售沪宁高铁票，应与相关部门正在酝酿优化沪宁高铁运行图有关。<br />
<br />
&nbsp; &nbsp; 沪宁城际高铁7月1日开行，到达南京最快73分钟。但沪宁间一站直达的极速高铁列车只有两趟，部分停站高铁列车耗时和一些动车组相当，但票价却与直达高铁没有区别，这引起部分旅客的抱怨。记者昨日了解到，近几日从上海出发的沪宁高铁客流不断攀升，但上座率仍有余量，部分车次开车前仍有约一半车票未售出。<br />
<br />
■现象<br />
<br />
高铁余票多 动车票难求<br />
<br />
&nbsp; &nbsp; 沪宁城际高铁7月1日开行恰逢铁路暑运开始。铁路部门预计，因学生客流、旅客客流的叠加，今年暑期上海铁路局客流与往年相比将有较大幅度增长。不过，大客流并未提升高铁上座率。<br />
<br />
&nbsp; &nbsp; 数据显示，开通以来，沪宁城际高铁客流不断攀升，7月1日当日乘坐旅客达9.4万人次，7月2日达13.8万人次，7月3日同样超过13万人次。由于统计数据包括沿线全部21个车站乘车人数，平均下来沪宁城际高铁仍然运力充裕。<br />
<br />
&nbsp; &nbsp; 记者昨日来到沪宁城际高铁的主要始发站――高铁虹桥站，看到前来乘车的旅客虽然不少，但偌大的候车室仍有近一半的座位是空的。记者通过查询获悉，高铁大部分当日车次仍有数百张余票。以G7170次列车为例，距离开车仅20分钟，二等座仍剩余300多张车票未售出，占载客量的一半左右。<br />
<br />
&nbsp; &nbsp; 和高铁列车形成鲜明对比的是，沪宁间保留的部分动车一票难求，近几日，南京方向的动车车票几乎全部售罄。昨天，虹桥站仍有不少人希望购买沪宁间的动车组车票，但未能如愿，只能转乘高铁。7月1日沪宁间25对直达动车已全部停开，虽然从上海仍有20余对动车组列车经停南京，但沪宁沿线城市都是这些列车的中途站，票源本来就不多。<br />
<br />
■抱怨<br />
<br />
部分停站车“速度慢”<br />
<br />
“沪宁高铁直达车是很快，但太少了。”记者昨日在高铁虹桥站遇到放暑假回家的小陈，他正在自动售票机前试图购买一张回南京的动车组车票。谈及为何不选择高铁，小陈表示，虹桥站除了下午3点的一趟直达车到南京仅需73分钟外，大部分高铁列车因沿途停站，到达南京所用的时间在1小时45分到2小时左右，比动车组快不了多少。但高铁列车不区分直达和停站车次，价格一律是一等座233元，二等座146元。“停站高铁比动车快那么一点，但票价却贵很多，性价比不高。”小陈解释说。<br />
<br />
&nbsp; &nbsp; 记者了解到，目前上海至南京高铁仅有两趟直达车，分别为上海虹桥至南京的 G7101/G7102高速直达列车最快73分钟 到 达 ， 上 海 至 南 京G7001/G7002高速直达列车最快75分钟到达。而其他车次跟动车组比较起来，速度优势的确不明显。以上海到武昌的D3018次列车为例，从上海到南京只需要2小时09分，和一些高铁停站列车相比只慢了 10分钟左右，与G7030次列车相比仅慢3分钟左右。但D3018上海至南京的票价为二等座93元，一等座112元，相比停站高铁更实惠。<br />
<br />
&nbsp; &nbsp; 采访中，有不少旅客呼吁，应该增加上海到南京的直达车数量，同时也可考虑增开一些直达无锡、常州等沪宁重要城市的直达车。另外，一些乘客也认为，直达车和停站车花费时间相差很大，现在实行票价“一刀切”似乎不大合理。<br />
<br />
■提升<br />
<br />
高铁运行图可能更改<br />
<br />
&nbsp; &nbsp; 沪宁高速铁路开通运营后，为了充分发挥上海虹桥站的运力优势，缓解上海站和沪宁既有线运力饱和的局面，开行的120对列车中有62对将安排在上海虹桥站到发，30对安排在上海站到发。记者在采访中了解到，无论是南京到上海的旅客，还是上海出发的旅客，多数更喜欢位于市中心的铁路上海站到达或始发，因为铁路上海站与市中心各主要区域之间集散更便捷，更省时。<br />
<br />
&nbsp; &nbsp; 上海铁路局相关人士表示，铁路部门一直在关注收集乘客对于沪宁城际高铁的意见，根据最近几天运行的情况，向铁道部作了汇报，建议优化调整沪宁城际高速铁路列车运行图。基于此，上海站暂停发售7月11日及以后的沪宁城际高速铁路线车票。其他车次的预售期不变。车站方面提醒广大旅客，要注意铁路部门的公告，及时调整出行计划。<br />
<br />
&nbsp; &nbsp; 对于沪宁城际高铁票价问题，铁道部发言人王勇平在接受记者采访时说，铁路部门正在广泛听取意见，高铁票价要接受市场检验，现在下结论为时尚早。<br />
