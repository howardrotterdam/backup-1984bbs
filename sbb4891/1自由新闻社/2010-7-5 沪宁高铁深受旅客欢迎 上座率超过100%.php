													<h2>天朝新闻奇观: 沪宁高铁上座率</h2>
						
						
																											<div id="postmessage_724362" class="t_msgfont"><strong>人民网：沪宁高铁受欢迎上座率百分百</strong><br />
<br />
<a href="http://news.qq.com/a/20100705/001698.htm" target="_blank">http://news.qq.com/a/20100705/001698.htm</a><br />
<br />
民网上海7月5日电（记者 沈文敏）今天从上海铁路局获悉，从7月11日起，该局对目前沪宁城际高铁开行的98对（其中18对为高峰日开行）时速300至350公里高速动车组列车开行方案进行优化调整。调整后，上海至南京间从早8时至晚19时，各整点都有一对直达高速动车组列车双向对发，在客流高峰期的9：30、13：30和15：30再增加3对直达高速动车组列车对发，使上海（上海虹桥）至南京间直达高速动车组列车全天开行达15对，比原来增加13对。<br />
据介绍，沪宁城际高铁自7月1日开通运营以来，深受广大旅客欢迎，日均发送高铁旅客约13万人，平均上座率超过100%。为更好地方便旅客出行，上海铁路局从7月11日起，对沪宁城际高铁开行的98对（其中18对为高峰日开行）动车组旅客列车开行方案进行优化调整。调整后，上海（上海虹桥）至南京间将安排开行直达高速动车组旅客列车15对，比原来增加13对，其中有6对安排在上海站至南京站间开行，车次为G7001至G7012次，分别在每天的8、10、12、14、16、18时整点对发，运行时间75分钟；有9对（其中3对为高峰日开行）安排在上海虹桥站至南京站间开行，车次为G7101至G7118次，分别在每天的9、11、13、15、17、19时和9：30、13：30、15：30对发，运行时间73分钟。<br />
同时，上海铁路局还对沪宁城际高铁上运行的其他列车开行方案进行了微调，压缩部分列车运行时分，铁路部门提醒广大旅客留意车站公告。<br />
另据了解，从7月5日20：00起，上海铁路局对沪宁城际高铁上运行的 “G”字头列车和 “D”字头列车的车票恢复11天（含当天）预售期。(人民网)<br />
<br />
---------------------<br />
<br />
<strong>扬子晚报: 沪宁高铁遇冷 平均每天每趟车仅卖出百张车票</strong><br />
<a href="http://www.ce.cn/xwzx/gnsz/gdxw/201006/27/t20100627_21552530.shtml" target="_blank">http://www.ce.cn/xwzx/gnsz/gdxw/ ... 0627_21552530.shtml</a><br />
<br />
昨天，记者在南京火车站看到，排队买票的人不少，可买高铁票的人却不多。不过沪宁线的客流向来是“随到随走”，客流究竟如何还要等高铁开行后才知道。另外，记者了解到，以后旅客们可以通过自动售票机、在窗口购买磁卡、在窗口购买车票等多种购票方式登上沪宁高铁。不仅如此，出站时持有沪宁高铁车票的旅客再也不用检票出站了，可以直接“刷票”。<br />
<br />
&nbsp; &nbsp; 售票<br />
<br />
&nbsp; &nbsp; 窗口高铁票为何“遇冷”？<br />
<br />
&nbsp; &nbsp; 可能是旅客习惯随到随走<br />
<br />
&nbsp; &nbsp; 昨天下午3:00，记者在南京火车站的售票窗口守了40分钟，却只碰到2个来买高铁票的市民，都是“尝鲜”去的。南京市民冯力行先生就准备7月1日起个大早坐上首班车，预计要在8点37分到达上海虹桥火车站。“即便以最紧凑行程，从火车站连接通道赶到虹桥机场的公交换乘枢纽转乘世博4路，换乘加上行车时间，大概1个小时才能直达世博园6号门。”不过为了体验高铁冯先生还是决定买高铁票。<br />
<br />
&nbsp; &nbsp; 不过记者在南京站售票厅里的屏幕上看到，7月1、2、3、4日高铁票的销售状况并不乐观。7月1日当天，G7003、G7025、G7011、G7013、G7014次(重联运行，核定1113/1218人)均余了一千多张车票。其余的高铁车次余票量在四五百张左右。2、3、4日的情况也是如此。就连最“火爆”的G7001次每天也只卖了100多张票。也就是说，平均每天每趟车只卖出了不到100张车票。不过沪宁线的客流向来是习惯“随到随走”，高铁的客流究竟如何，还是要等到开行当天才能看出来。<br />
<br />
&nbsp; &nbsp; 购票<br />
<br />
&nbsp; &nbsp; 高铁票可窗口购买、自助购买，还可刷卡乘车<br />
<br />
&nbsp; &nbsp; 昨天12点，记者来到南京火车站一楼售票大厅时看到，所有的购票窗口前都排起了长队。位于大厅左侧，两台为沪宁高铁准备的自助售票机上还贴着泡沫保护膜，尚未使用。南京火车站内的这两台自助购票机与别的站点略有差别，自助购票机上，只设了银行卡插口、现金插口。记者了解到，目前，自助售票机上不但没有其余的购票方式，就连银行卡插口也还处于“摆设”状态，购票机投入使用后，市民暂时还只能通过现金插口用纸币购票，面额仅限100、50、20、10、5元的纸币，硬币不能用。<br />
<br />
&nbsp; &nbsp; 据了解，沪宁高铁今后可能参考京津、广深城铁模式，为旅客提供刷卡乘车服务。在一些铁路部门经营的场所，旅客还可以持此卡消费。“就像现在的金陵通市民卡一样。”<br />
<br />
&nbsp; &nbsp; 连日来，记者在江苏多个高铁站售票大厅内看到，不少高铁站里的自助售票机上的插口远多于南京站这两台机器的插口。除了铁路快通卡、银行卡、现金插口之外，还有一个二代身份证的感应区。乘客在自动售票机上用身份证购票时，需要将二代身份证进行扫描，完成支付后，旅客可以取磁票，也可以不取磁票，如果市民不取磁票，可直接持二代身份证检票上车。“这个感应区是为火车票实名制预留的，目前也都用不着。”铁路部门工作人员告诉记者。<br />
<br />
&nbsp; &nbsp; 出站<br />
<br />
&nbsp; &nbsp; “G”字头车出站有何便捷？<br />
<br />
&nbsp; &nbsp; 将来可以“刷票”出站<br />
<br />
&nbsp; &nbsp; 昨天，记者在南京火车站的出站口，看到了12个上部橘黄色、下部金属色的刷票机。尽管刷票机目前尚未启用，但相关人士表示，7月1日以后旅客乘坐沪宁高铁可以在出站口通过自动检票机快速“刷票”通过，无须人工检票程序。“到时候会对客流进行分流。持有高铁车票的乘客可以从刷票机前刷票通过，普通车票的旅客从旁边通过。”另外，如果客流量较大，“刷卡通过”亦可改为“人工检票通过”。<br />
<br />
&nbsp; &nbsp; 提醒<br />
<br />
&nbsp; &nbsp; 学生证不能优惠咋回事？<br />
<br />
&nbsp; &nbsp; 有直达车就不能分段购买<br />
<br />
&nbsp; &nbsp; “我有学生证，凭什么不卖我高铁的学生票？”随着各大专院校陆续放假，购买学生票的旅客越来越多。记者昨天从南京站获悉，不少同学因为“区间不符”而不能享受半价优惠。按照铁路规定，学生票要严格按照学生证上的乘车区间发售，不能分段购买。只有学生证上写了“南京-上海”的学生，才可买到高铁109.5元的高铁票。另外，学生票应按近径路或换乘次数少的列车发售，但在直达列车后换车次数少的远径路也可发售。学生回家或返校，全程旅行需要乘坐其他交通工具时，在不绕道原则上，经确认后可分段购买学生票。<br />
<br />
&nbsp; &nbsp; 这个规定看起来前后矛盾，实际上应该这样理解：不能分段购买是指，比如家在广州的同学，不可以买南京-武昌、武昌-广州两段学生票，因为南京到广州有直达车。所谓可以分段购买学生票，比如家在辽宁辽阳的同学，辽阳离鞍山25km，离沈阳67km，辽阳到南京没有直达列车，只能就近到鞍山或沈阳去坐车，这种情况下就应该可以买鞍山-南京或沈阳-南京的学生票。</div>

							
							
							
							
