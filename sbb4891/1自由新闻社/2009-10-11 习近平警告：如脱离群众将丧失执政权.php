													<h2>习近平撰文论述党建保证长期执政为重点</h2>
						
						
																											<div id="postmessage_277793" class="t_msgfont">记者: 燕青 | 华盛顿 2009年 10月 11日<br />
<br />
目前正在欧洲访问的中国国家副主席习近平在中国官方的人民日报发表了一篇将近两万字的文章，专门论述新形势下党的建设，引起国际媒体的关注。<br />
<br />
习近平以《加强和改进新形势下党的建设的纲领性文献》为题，在人民日报的网站上对十七大四中全会上审议通过的《中共中央关于加强和改进新形势下党的建设若干重大问题的决定》，进行了比较详细的解说。<br />
<br />
*马克思主义中国化？*<br />
<br />
文章在很多地方强调了马克思主义，说“坚持把思想理论建设放在首位，提高全党马克思主义水平”是中国共产党自从执政以来加强自身建设的第一条基本经验。但是文章对马克思主义到底是什么、如何解释，看来并没有做出详细说明。<br />
<br />
在“加强和改进新形势下党的建设的新要求新举措”这一栏下面，文章说，当前需要围绕“四大问题”，不断作出新的理论概括，增强理论说服力和感召力，并进一步解释说，这四大问题就是：什么是马克思主义、怎样对待马克思主义，什么是社会主义、怎样建设社会主义，建设什么样的党、怎样建设党，实现什么样的发展、怎样发展。<br />
<br />
*保护执政党利益*<br />
<br />
由此似乎可以看出，中国共产党对主义的拴释以及为了实现目标而采取的策略都可以商榷。<br />
<br />
香港浸会大学的政治学主任教授卡博斯坦教授分析说，对马克思主义的基本含义提出有拴释的余地，这从前是没有过的。<br />
<br />
卡博斯坦教授说：“文章说‘要推进马克思主义的中国化、时代化、大众化’，这很有意思。过去从来没有说过要把马克思主义中国化。”<br />
<br />
这位长期关注中国政局的专家说，习近平在人民日报网站上发表的长篇大论，是以他中央党校校长的身份撰写的，纵观全文，党内民主谈的并不多，重点还是强调在新形势下如何保持、进而保证共产党的领导。<br />
<br />
卡博斯坦教授说：“把马克思主义中国化，估计也是想用所谓的中国国情来保护执政党的利益，从而控制党内民主、以及全社会多党制的发展。”<br />
<br />
*担心“人亡政息”*<br />
<br />
从习近平的文章中也可以看出，中国共产党对自身的处境是有一些担忧的。在强调“执政能力建设和先进性建设”的时候，文章指出：“最近20年世界上一些执政多年的大党、老党丧失执政地位、特别是一些马克思主义政党人亡政息的惨痛教训表明：一个政党的执政地位不是与生俱来的、也不是一劳永逸的，过去拥有不等于现在拥有，现在拥有不等于永远拥有”。<br />
<br />
但是，尽管有这种担忧，习近平这位中央党校校长兼国家副主席日前发表的这篇文章，通篇的论述，围绕的要点是如何保证目前的执政党“永远拥有”操控中国大局的权力。<br />
<br />
当被问到习近平将来一旦成为中共领导人是否会成为前苏联的戈尔巴乔夫或者是叶利钦这样的政治改革派时，香港浸会大学的卡博斯坦教授说，话不能说死，但是，仅从这篇文章里，似乎看不出这种迹象。<br />
