<br />
整个暑假，李海龙的心情一直处于低谷。他在舟曲县第一中学读书，一分之差，让他无缘本科。高考失利，让他愧疚和自责，不知道怎样面对家人，他独自留在县城，没回舟曲峰迭乡老家。<br />
<br />
7日，天下着蒙蒙小雨，已是晚上十点，李海龙依然在小城的街巷里漫步。高考，让18岁的他成熟了不少，连续几日，他一直在思索未来，想给自己一个答案。<br />
<br />
蹲在街巷拐角的阴影里，李海龙拿出手机，决定给爸爸打个电话。“爸爸，对不起。”本想不哭，可不争气的泪水却流满了他的脸颊。<br />
<br />
10点40，他回到城管第一小学附近的一家小旅馆，睡下了。打过了电话，李海龙感到轻松了很多。“我想回家。我要回家。”李海龙迷迷糊糊的睡着，在梦里对自己说。<br />
<br />
“轰隆隆……轰隆隆……”窗外是刮大风了吗？“咚隆隆……咚隆隆……”屋顶上的东西掉了吗？李海龙翻了翻身，不想起来，只在朦胧中猜测着。但是，外面的声音却越来越大，有如同坍塌一般的轰鸣声，有如同爆炸一般的巨响声，有如同海浪一样的流水声，有如同地震一样的裂变声……<br />
<br />
李海龙猛然从床上坐起来，裹着被子，本能的往家门口跑。可是，打开房门，他不由得倒吸一口冷气，往后退了几大步……<br />
<br />
耀眼的闪电在天空中狰狞着面孔，家门前那棵高高的大柳树，那些如繁星般点点的村落，都被粘稠的石流洪浪所吞噬；很多人蹲在屋顶上凄厉得哭叫，有的人前一刻还挥着手，几秒后，便不见了踪影……<br />
<br />
可怕的景象把他逼回了房间，躲在桌子下面，李海龙瑟瑟发抖，他感觉到死亡已经攥住了喉咙……<br />
<br />
“我要活着……我要回家……”――这样的念头从脑海深处迸射出来。<br />
<br />
但是，屋前门后，泥浪翻滚，根本无路可走！而这时，四面的墙壁在颤抖，大块的瓦楞往下砸，如果再不出去，房屋倒塌，他必死无疑！<br />
<br />
绝望中，李海龙抬头看到了屋顶的天窗。对！从天窗出去！出去后就是相对较高的屋顶！<br />
<br />
李海龙爬上了床，他的手够到了天窗。但是，天窗外有钢筋焊接的防护栏，没有钳子斧子，怎么能出去？<br />
<br />
“我一定要回家！”这个简单的信念让他变得异常执着，充满了力量。<br />
<br />
李海龙用手疯狂的摇动着天窗上的钢筋，零散的泥沙掉进他的眼睛里嘴巴里，钢筋将他的手磨得生疼，但是他已顾不得这些！一下，两下……坚硬的钢筋防护栏纹丝未动，可李海龙依然没有放弃这唯一的希望。<br />
<br />
使出浑身的力气，他奋力扭动着像恶魔一样挡住生路的钢筋，求生的渴望让他不顾一切。突然，一根钢筋竟然被扭断了，李海龙探出了脑袋！顾不得钢筋穿骨的疼痛，李海龙将身体从狭窄的窗户中硬生生得挤了出来！<br />
<br />
可是从天窗出来，面对汪洋泥海，李海龙再次感到绝望。<br />
<br />
环顾四周，他发现了一条生路――泥石流没有淹没家后那些平房的房顶，泥海中星星点点露出的屋顶，就是逃生的出路！<br />
<br />
迈开双腿，鼓足勇气，李海龙小心翼翼的跳着房顶，竟然从泥海逃了出来！<br />
<br />
“那时我不知道自己哪来的力气！第二天，我再次试图用手掰断钢筋，却再也做不到了……”事后，李海龙也对掰断钢筋逃生的经历感到不可思议。“也许那是人爆发出来的潜能……”他说只能这样解释。<br />
<br />
8日傍晚，李海龙见到了从老家来找他的爸爸、叔叔，“我们都觉得像是在天堂相见一样！他们以为我死了，我以为再也见不到他们了……”李海龙这样说到。<br />
<br />
大人抱着年幼的孩子，儿女拖着年迈的父母，逃出来的人为自己失散的亲人痛哭流涕，死难者永远被泥浪封住了鲜活的生命。李海龙说，他的脑中永远也抹不去这一幅惨痛的生命长卷。<br />
<br />
“我看到一个已经逃出来的叔叔抱着一个光身子的小孩在痛哭，因为就在不远处，他的大女儿和妻子在家里冲他挥手喊救命……”李海龙说，虽然当时自己很害怕想尽快逃命，但是他实在不忍心眼睁睁的看一家人生离死别。<br />
<br />
“来，叔叔，你去救他们，孩子我帮你抱到二郎山！”李海龙伸出双手从挥泪的父亲手中接过了孩子。<br />
<br />
“我背着小孩奔到了二郎山上，那个叔叔则去救他的家人。后来，在山上我把孩子还给了他，所幸，他们一家都活了下来。”说起逃生路遇相助的那一家人，李海龙从心底里感到欣慰。<br />
<br />
经历了生死，李海龙特别想回家。但是，一想到自己遇难的同学，一想到那些泥石流下掩埋的生命，他却决定要留在县城。<br />
“我和几个幸存的同学当志愿者，我们都憋着一股气，拿铁锹没命的挖……”李海龙流着眼泪说,“我有一个关系很好的朋友，今年刚考上了中国石油大学，可是还没来得及高兴庆祝，一家人就都死了……”<br />
<br />
“想起他们，我觉得好难过，活下来，就是最大的幸运。”劫后余生的李海龙，对生命的价值有了全新的体会。<br />
<br />
19日，李海龙终于回家了。这次回家的路，他走得太惊险，太漫长……<br />
