&nbsp; &nbsp; 2009年7月16日周四下午17：00，我又一次抵达日本成田机场，准备乘晚上19：05出发的美国西北航空公司NW27航班回国。这是第六次回国闯关。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 日本电视台记者小林先生已经在机场大厅等候，他特意来全程采访美国西北航空公司拒载乘客的事件。这件事件已引起国际社会的很大关注，成为一个三国四方的人权事件，其背景就是美国西北航空公司在日本成田的国际机场上配合上海当局阻扰一个中国公民回国、侵犯人权。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 在成田机场美国西北航空公司登机办理处，我又一次与美国西北航空公司的承办经理对峙两个多小时。我有护照、当日机票等合法证件，但美国西北航空公司仍然拒绝我登机，还是没有任何合法的拒载理由，即使说上海当局不准我入境回国，也无法出示一张上海官方的文书。所以，这位经理只好挨骂赔笑，美国公司一次一次在日本的国际机场上丢丑现眼。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 美国西北航空公司连续三次成功阻扰一个中国公民回国，应该说它协助中国权贵迫害中国公民的行动中立了大功，如果没有我强烈的抵抗，或许它现在可以向上海当局请功领赏了。但是，它现在碰到麻烦了。一个不屈服中国权力的人，同样也不买账美国的资本家，我作为一个中国人也要有尊严地活在这个世界上，维护自己及其他人的基本人权。在世界上，向权力献媚的资本家许许多多，但是勾结权力迫害一国公民的公司还是为数不多，美国西北航空公司就是一家。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 类似美国西北航空公司这些外国大公司在经营上情商极高，但在中国问题的政治判断力上智商极低，还不如中国的一个访民。它们与中国的交往中还是老一套，巴结权势者，轻视民众，以为中国是没有法律的，政府官员的命令就是法律，只要讨好政府官员，就可以事事顺利。其实，中国从2003年就开始变化了，法律制度已基本建立，民众就是利用法律抵抗官府的侵权与压迫，现在的中国已是官怕民。与中国交往的外国公司应当依法经营、保持中立，才可以顺利运营，否则麻烦不断。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 现在，美国西北航空公司的承办经理糊涂了。中国政府至今没有声明禁止冯正虎回国，驻日大使馆也没有承认这个事，连上海政府都没有一个官方文书。但是，上海边防警察部门确实电话口头通知美国西北航空公司上海支店，不准冯正虎入境，要求航空公司配合，据称这也是上海政府的决定。这位承办经理与我对话中，突然发问：中国怎么有两个政府？我们也不知听哪一个好。美国西北航空公司放弃中立原则，势必卷入一个致命的麻烦漩涡中。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 日本政府国土交通省（类似于中国的交通运输部）已对拒载乘客的中国国际航空公司、美国西北航空公司立案调查。中国国际航空公司回复日本国土交通省国际航空课官员的拒载理由是，运载冯正虎回国已遭到上海当局的罚款。日本方面首先要求它出具被上海当局罚款的官方文书，以示证明这个事实的真实性。根据中国的行政处罚法，公司既然遭到罚款，肯定有官方的罚款处罚书。拿不出证明自己行为有理由的这些官方文书，中国国际航空公司、美国国际航空公司拒载乘客的行为肯定要受到行政处罚。而且，美国国际航空公司注定是上海当局违法事件中的牺牲品，它侵犯人权的行为将会受到美国的追究。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 一个中国人的回国回家，竟然是如此艰难，连续六次均被挡在国门外。不仅是强大的中国权力，连富有的美国资本，也都热衷于压迫一个弱小的个体，无情无义无理无法，践踏人权。这是世界的悲哀，中国的悲哀、也是我个人的悲哀。但是，我不会屈服，为了维护中国国家与宪法的尊严，为了捍卫中国公民回国回家的自由权利，我会坚持不懈地抗争到底。我深深感谢中国民众的支持。无论上海当局如何打压，无论我是否能回到上海，上海民众都来机场欢迎我回国。而且，全世界的中国公民、海外华人已开始呼吁与行动，让我们共同消除每一个中国人回国出国的恐惧，让中国人都可以自由往来自己的祖国，让祖国变得更美好、更亲切。</div>

							
							
							
							
