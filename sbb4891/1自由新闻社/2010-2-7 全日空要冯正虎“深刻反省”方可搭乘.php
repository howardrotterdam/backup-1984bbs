&nbsp; &nbsp; VOA记者: 申华<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;日本航空公司“全日空”(ANA)表示，上海维权人士冯正虎要搭乘他们的飞机返国必须“深刻反省”，因为他先前搭机过程中的行动干扰了其他乘客，影响了飞行安全。冯正虎的回国机票目前还没有落实。 <br />
<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; *冯正虎被指干扰航班运行*<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 上海维权人士冯正虎入境日本后，立即收到了日本航空公司“全日空”发来的一封信。信件以日本和中文两个版本送交冯正虎。由于正值周末，记者无法联系到全日空了解情况，不过，冯正虎接受美国之音采访时，记者请他宣读了全日空公司2月3号信件的中文原件。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 谈到冯正虎2009年11月3日搭乘全日空回国，后被中方原机遣返一事时，全日空指责冯正虎在机舱门口拒绝登机，并在浦东边防检查站人员陪同时大声喧哗，使其他众多旅客倍感不安，数名旅客因此投诉全日空。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 信件还说，事件使那次航班延误50多分钟。信件还就冯正虎滞留机场行动发表了评论，称冯正虎拒绝进入日本国，不办理入境手续，长期进行静坐示威。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; *全日空称按照中方指示行事*<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 全日空致冯正虎信函的另外一个要点是，该公司称，对冯正虎所采取的一切行动都是遵循中国方面的指示。冯正虎念道：“本公司再次重申，针对冯先生采取的一切措施，都是根据中国当局的指示执行的。冯先生的一连串行为是严重扰乱机内秩序，妨碍安全的行为。同时，对冯先生在上海机场和成田机场的行为也感到极其遗憾。”<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 这封信最后说，冯正虎承诺不再发生类似行为，强烈表示“深刻反省”，挽回与全日空的信赖关系前，全日空将谢绝其搭乘。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; *全日空是发难“急先锋”*<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 入境后正在休整的冯正虎说，全日空是入境日本后对他发难的“急先锋”，颠倒黑白，因为全日空高层官员在冯正虎事件期间曾经给他发过道歉信。报导说，离开成田机场时，还有全日空人员友善地为他送别。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 冯正虎说：“信件至少反映两个问题。第一，全日空说，我是在登机门口外(抗争)，机舱内我和其他普通乘客是一样的，没有吵。全日空是以书面形式颠倒黑白，其动机和背景我不清楚；再有，我回国买机票被堵住了，我觉得这种做法根本不像一个商业公司的做法，它想直接参与到政治里面去了。”<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; *全日空欲甩掉热山芋？*<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 全日空谢绝冯正虎乘机的消息传到上海，正在筹备迎接冯正虎回国的上海居民童莉雅对美国之音说，全日空可能先发制人，以此割断与冯正虎的关系，不再涉足这个敏感的政治事件。她说：<br />
&nbsp; &nbsp; “(全日空)其实想为难冯正虎先生，希望他最好不要在这家公司买票了，全日空觉得，冯正虎是一只烫山芋”。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 冯正虎对美国之音说，春节前回上海的计划没有变，但是机票还没落实。全日空横生的枝节是否就是冯正虎回国前的全部目前还不清楚，预计他星期一将对外宣布有关回国的最新情况。</div>

							
							
							
							
