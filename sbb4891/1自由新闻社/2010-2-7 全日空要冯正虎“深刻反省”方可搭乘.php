													<h2>冯正虎：抵抗全日本航空公司（ANA）</h2>
						
						
																											<div id="postmessage_472943" class="t_msgfont">2010-02-08<br />
<br />
谢谢你的关心与支持。<br />
 <br />
我定于2月12日上午09：45乘日本航空（JAL）JL791航班，中午12：20抵达上海浦东机场。<br />
附上我对全日空航空公司信函的公开答复。<br />
今天下午3：00在日本记者俱乐部一楼大厅与部分记者会面,不是正式的记者会。<br />
 <br />
冯正虎<br />
<br />
<br />
 <br />
 <br />
抵抗全日本航空公司（ANA）<br />
<br />
冯正虎<br />
<br />
 <br />
<br />
 <br />
<br />
2008年2月3当我结束92天露宿日本国门外的生活入境日本准备回国时，中日两国政府都感到轻松，我与上海当局的对峙暂时化解了，中国人的内斗有希望和解，而且由此将产生中日外交的麻烦也可以避免了。但是，参与绑架活动的全日本航空公司（简称ANA）却不满意，直接向我抛出一份挑衅式的公司意见书，又来落井下石、浑水摸鱼。我居然没有想到，这家日本公司怎么比上海的违法官员更无耻，伤害他人后不但不表示歉意，反而还要受害人向他道歉。<br />
<br />
我结束露宿日本国门外92天后的第2天，即2月4日在我妹妹家里收到ANA2月3日寄出的公司意见书（中日文），ANA做了隐瞒事实、颠倒是非的论述后，通告我：“就冯先生今后选乘本公司航班的事宜，本公司在未能判断冯先生已经对本公司表示了诚意，承诺不再发生同样行为并强烈表示深刻反省，且尽力挽回与本公司的信赖关系前，本公司将予以谢绝搭乘并特此通知。”<br />
<br />
或许，ANA以为我已经离开机场，没有压力了，它又可以积极讨好上海的一些违法官员，继续作恶。我回国，需要乘飞机，不可能游过东海，但我不会向这些乘人之危从中取利的日本奸商低头。我郑重地公开通告：我愿意接受ANA的挑衅，让世界公众都来评判ANA的公司意见，并开始抵抗侵犯人权的ANA，直至它赔礼道歉。我相信：我的中国同胞以及尊重人权的所有外国人，包括日本人都会支持我的抵抗。<br />
<br />
 <br />
<br />
 <br />
<br />
一、&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;ANA参与上海违法官员非法强制遣送及绑架的事实<br />
<br />
 <br />
<br />
 <br />
<br />
我与ANA是有缘份的。2009年6月7日第一次回国被拒绝入境，非法强制遣送到日本，被抛在远离东京的大阪关西机场，上海警察用的是ANA的航班；11月3日第八次回国被拒绝，第二天上海警察伙同ANA上海支店的职员使用暴力手段，将我非法绑架至日本的飞机，也是ANA的航班。<br />
<br />
 <br />
<br />
 <br />
<br />
1.&nbsp; &nbsp;&nbsp; &nbsp;第八次回国被拒绝入境的全过程<br />
<br />
 <br />
<br />
 <br />
<br />
2009年11月3日下午我与我妹妹，还有一位朋友一起在成田机场的ANA柜台上办理入境手续。当时一位ANA的职员告诉我：“上海方面通知不能让你回国。他们说，如果你回国，晚上旅馆费用要你付，而且还要付两间的房费。”我妹妹将我的话翻译给她：“这是我与上海当局的事，到上海让他们自己对我说，与你们航空公司没有关系。你们航空公司是否同意我搭乘？” ANA的职员说：“我们同意的，你的证件都齐全。”她办理了我的登机手续，接着我顺利出境，登上NH0921航班回国。<br />
<br />
我很清楚，上海当局还没有愚蠢到向被绑架者开口勒索非法拘禁场地的房费，他们惯用的做法是事后用钱与被害者和解私了。他们如果说这些话，也仅是吓唬ANA而已。或许，他们不承认说过这些话，这是ANA自己编造的。我知道，ANA当时肯定不敢用上海当局不让我回国的理由来拒绝我搭乘，因为有两家在日本经营的外国航空公司已由此违法行为被告上日本法庭正在审判中，而且ANA是日本企业，在日本本土上是不敢公然触犯日本法律的。<br />
<br />
当日晚上21：10许， 飞机停靠后，机舱门一打开，我就被等候已久的一大批警察带走，经过搜身查物品，然后又被告知：“冯先生，这次又不可以入境回国，这是上级领导的决定。”我 问他：“你这个上级领导究竟是谁？这样重大的处罚怎么可以空口无凭，连一张正式文书都无法出示呢？你们的行为是违法的。”他也无法答复，只是苦笑。晚上我 被强制住宿在上海浦东机场内的宾馆。<br />
<br />
第二天是11月4日，大约上午9：45，十几个违法的上海警察又将我强行送至ANA的NH0922航班的停靠处，企图又一次非法“遣送”一个本国公民去外国。我强烈抗议，他们使用暴力手段企图将我绑架至飞机内，我竭力抵抗，死守飞机的登机口，大声呼救：“绑架，你们是犯罪！”，与这些绑匪搏斗了一个多小时。最后，ANA上海经理饭田屈服于绑匪的威胁，协助他们的暴力绑架行动，四个年轻力壮的便衣警察硬将我抬着拖至机舱底部的座位，一位身材高大的ANA上海支店职员用他的全身将我压在座位上，这时我已经筋疲力尽，无力抵抗他们的违法犯罪行为。乘务员匆匆拉上机舱门，飞机驱向跑道，急忙起飞。航班上有些乘客亲眼目睹我被抬着拖着的暴力绑架场面，飞机也由此延误56分钟起飞。<br />
<br />
机舱门一关闭，我就马上冷静了。这位参与绑架的职工也松开我，并当场向我赔礼道歉：“这样做很不对的，我也没有办法，是拿工资的人，只好听从命令，请原谅。”我告诉他：“我不会怪你的，我会追究指挥人的责任。你们ANA不顾飞机上旅客的安全，把一个中国公民强行绑架在飞机上，但我会顾全旅客的安全。你放心，一路上不会有麻烦。” 在飞机内，我的行为与所有乘客一样，连抗议声都没有，坐在位子上闭目养神，平静地抵达成田机场。<br />
<br />
