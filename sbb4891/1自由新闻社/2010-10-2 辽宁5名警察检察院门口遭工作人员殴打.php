<br />
本报讯 （记者褚朝新）昨日，一位身着警服的男子与人扭打的照片在网上流传，经查证，事发地为辽宁鞍山市检察院门前，当地5名公安警员在与检察机关交涉一起对警方的指控时，双方发生争执并大打出手，随后网民将拍到的情景发布到网上。昨日，鞍山市公安局和检察院都证实有此事，并表示正在进一步调查此事。<br />
<br />
警察打出横幅称遭迫害<br />
<br />
昨日，发布照片的网友爆料称，9月29日早，辽宁鞍山市检察院门口聚集了很多人，场面混乱，胜利路交通被堵塞，5名身穿制服的警察被市检察院的人围攻并打倒，昏迷的警察随后被拖进检察院大楼。<br />
<br />
网友发布的照片同时显示：事发前，5名身着警服的男子打出抗议横幅，指检察院迫害保护国有资产的公安干警。<br />
<br />
鞍山检方一知情人士透露，冲突是因为鞍山市检察院反渎职侵权局指控鞍山市公安局钢都分局5名民警，在办理案件中存在刑讯逼供的行为。因对指控存在分歧，5名警员来到检察院大楼前扯起横幅。此说法也得到了鞍山市公安局有关人士证实。随后，公安警员与该院一名司机发生争执和打斗。该司机被殴后，检察院工作人员又把警察打了。<br />
<br />
检察院称司机参与冲突<br />
<br />
鞍山市检察院纪检组长王永杰称，打架的事情检察官没参与，是检察院个别司机与公安民警冲突。今日，该院将开会讨论如何处理此事。对警方提及的对刑讯逼供处理不服等情况，王永杰表示暂不知情。<br />
<br />
