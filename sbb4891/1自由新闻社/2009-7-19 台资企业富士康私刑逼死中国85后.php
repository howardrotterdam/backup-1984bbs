																											<div id="postmessage_171242" class="t_msgfont">富士康25岁员工因手机样机丢失跳楼自杀 <br />
<br />
<br />
<br />
据《南方都市报》报道7月16日凌晨，富士康科技集团（深圳市宝安区）年仅25岁的员工孙丹勇，从12楼跳下身亡。此前，公司交由其保管邮寄给苹果公司的16部苹果iPhone样机少了一部，孙曾接受公司环安课调查。18日，孙丹勇的大学同学指称，孙在接受调查时遭到非法搜查、拘禁和殴打。 <br />
<br />
<br />
&gt;&gt;同学 <br />
<br />
孙遭殴打非法拘禁<br />
<br />
毕业于哈工大的孙丹勇年仅25岁，7月16日凌晨3时33分许，从12楼跳楼自杀。7月18日，一位网友自称是孙的同学，发帖指称孙丹勇的死跟富士康在调查整件事情中非常规的手段有关。<br />
<br />
网 帖称，孙丹勇主要负责富士康公司苹果iphone第四代N90的导入，在给苹果公司寄产品时，一共为16台机台，但客户只收到15台，苹果公司因此怀疑富 士康可能泄密。这给富士康带来非常大的压力。因为孙丹勇负责邮寄机台，他摆脱不了嫌疑，所以环安课的便拉他去盘问。其间，孙丹勇曾遭殴打、非法搜查、非法 拘禁等。<br />
<br />
&gt;&gt;警方 <br />
<br />
监控拍下自杀过程<br />
<br />
据悉，富士康集团安全管理处早已对此事展开调查。一份情况呈报叙述了调查经过。<br />
<br />
据 富士康提供的孙丹勇自述材料提及，孙丹勇在7月9日从生产线拿到iphone，清点数量后暂存。7月10日，取机台的人来，孙丹勇打开纸箱让他们确认数 量，孙有事不在场，回来后发现少一台，孙怀疑机台丢在产线，但没能找到。随后寻找仍无果，13日下班时上报主管。部门在查找未果后，于15日上报环安课。 7月15日，3名富士康员工到孙丹勇租住房屋搜查。<br />
<br />
孙跳楼自杀后，警方在孙丹勇的手机中发现他向多名好友发的信息，称因其管理的N90苹果手机丢失，遭到公司怀疑，并被调查和搜查，心里想不通。<br />
<br />
据观澜福民派出所参与办理此案的李姓民警介绍，监控录像显示，孙丹勇自杀时间为7月16日凌晨3时33分52秒。孙租住楼楼下的监控摄像头拍下了其跳楼自杀的一幕。对于环安课顾姓课长非常规手段的调查细节，警方表示目前尚无定论，但已经介入调查。<br />
<br />
&gt;&gt;富士康 <br />
<br />
涉事课长已被停职 <br />
<br />
富士康科技集团行政总经理暨商务长李金明接受采访时说，他代表本人及集团，对员工孙丹勇自杀事件的发生表示痛心和惋惜。<br />
<br />
针对网络盛传的富士康某顾姓环保课长在追查产品丢失过程中对孙丹勇采用不适宜手段及做法一事，如搜家、关禁闭等，李金明表示，公司欢迎社会舆论来帮助富士康检讨管理上存在的不足，公司也已开始对大家关注的某些问题展开调查。<br />
<br />
目前，公司已对网络中提及的所谓顾姓环保课长给予停职停薪，并交由公安部门进行调查。李金明表示，富士康会尽力与死者家属一道做好死者善后事宜。</div>

							
							
							
							
