你在天堂还好吗？――祭奠我的学生孙丹勇《来自我们的辅导员》<br />
<br />
孙丹勇，哈工大管理学院2008届毕业生，家在云南省宣威县龙潭镇得福禄村一社，我的272个学生之一。<br />
　　　　<br />
　　　　刚刚接手这个年级的时候，我对他几乎没有印象，直到开始去掌握每一名贫困生的情况时，从年级、班级学生干部的口中我才知道了他。他们告诉我，丹勇冬天从来都不穿棉衣、棉鞋，就是一身单衣、一双单鞋过冬，即使给他发了补助，让他去买，也不见他身上的衣服能暖和多少，每每问起，他都说不冷。这样的举动，让我觉得很奇怪，于是慢慢地，我开始找机会去接触他，试图去了解他。可每一次见到他，他都是羞涩的一笑。和他谈话的时候，他并没有太多的言语，而且还略显紧张，所以基本上都是我在说，他在听，听的时候他的眼光并没有聚集在我的身上，但我知道我的话他记在了心里。<br />
　　　　<br />
　　　　又到了冬天，我找人给他买来了棉衣棉鞋，至于有没有穿，我现在都已经记不太清了。还有几次，都是从他们寝室同学那里得到消息，说他饭卡里的钱已经很少了，没有钱加，每天都吃的很少。每一次给他发完生活补助的时候，我都会告诉他身体是最重要的，一定要吃饱，有什么困难找老师。可每一次他还是自己饿肚子，自己吃苦。因为在他的心里，他没有觉得这是在吃苦，他知道年级的补助是有限的，还有很多同学和他一样需要帮助，他不愿意因为自己影响别人。我想这也是为什么他在决定离开的时候，还在想着自己的助学贷款，还在想着别影响到师弟师妹。<br />
　　　　<br />
　　　　丹勇没有担任过任何的学生干部，但在我的印象中，也从来没有缺席过年级和班级任何需要他参加的活动。他对集体关心的表达方式就是一直在默默的做着他觉得自己应该做的事情。也有一次他给我发邮件，说年级和班级的事情，没有署名，我想他是觉得在给我提意见吧，所以不愿意落款。其实看完邮件我就知道是他，但我一直没有揭穿。当时我觉得他能给我发邮件，是对我的信任，也是他的进步，既然他选择了匿名这种方式，我就应该尊重他，以免打消他的积极性。<br />
　　　　<br />
　　　　接触多了，我也逐渐理解了丹勇内向的原因。其实和很多贫困生一样，从云南山村来到这个现代化的大都市，环境的变迁让他觉得诚惶诚恐，置身于高楼大厦之间，仿佛迷失了方向。虽然没有攀比的心理，但是和同学之间的物质生活上的差异，还是让他觉得很自卑。同学们都是多才多艺，自己高中时学习上的优势体现不出来，甚至在班级都变成了中等。面对这些，本来就不爱说话的丹勇，变得更加沉默。但是沉默不代表放弃。他说自己一直在努力，虽然很多方面自己不如别人，但是自己已经在尽力做到最大的付出了。<br />
　　　　<br />
　　　　随着大学生活的继续，心理上的冲击也越来越小，丹勇对自己的认识也越来越清晰，对自己的定位也越来越明确。等到大四的时候，丹勇很快就选择了找工作。说实在的，他对自己的就业很担心，我对他也很担心。可是出人意料的是，工作找的很顺利。在他来找我签协议的时候，我告诉他社会和学校不一样，除了要保持自己的努力和认真以外，还一定要让自己开朗起来，多和人交流。那时候，他的笑容很灿烂，但依然带了些羞涩。然而谁又能够想的到，就是这样一份协议，居然开启了他生命倒计时的时钟。<br />
　　　　<br />
　　　　丹勇，我知道一个农村的娃，要想走出大山，考上大学，顺利毕业是多么的不容易，要吃多少苦，要留多少汗，要承担多大的压力，所以一直以来你都是面黄肌瘦，显得弱不禁风。你总是那样的内敛，总是那样的沉默，总是喜欢用行动来表明自己。生命有长短，却没有黑白，离去了或许是解脱，或许是种表白！<br />
　　　　<br />
　　　　 悲哉！惜哉！<br />
　　　　<br />
　　　　 丹勇，你在天堂还好吗？<br />
