<br />
第二，也是去年，我坐火车回家，遇到一位在烟台富士康PSP厂的流水线女工，闲着无聊就聊了一会。她讲的(我没去验证)：月薪1500，三险一金，包食宿（也可以自己出去租房），有奖金，每月工作22天，分早中晚三班，每班8小时，晚班工资略比早中班高点，加班有二到三倍加班工资（详参《劳动法》），以及各种补助云云，算下来，每月能拿到1800~2500元。逢年过节还有红包，老板亲自来发。谈话间，她不止一次的流露出对工厂里中方管理人员的厌恶。<br />
<br />
第三，今年春节，我去外地过年，火车上遇到几个一汽丰田天津厂的中方主管。这些人在我的铺位喝酒（我胖而且腰不好，所以长途一般都是软卧下铺，短途就动车或软座），酒后开始聊他们厂里的那些破事。其中有一个我印象很深的片段，就是去年春天，丰田一个日方高管到天津厂驻点，快回日本的时候开了个会，提出一个建议，说一线工人的酬劳太低了，连他自己都觉得不好意思了。虽然中方这边都反对，但是还是提交到天津劳资部门去了。我上铺那位，据说是厂里主管生产的，突然愤愤不平起来，“老子刚到一汽做学徒那会，一个月才拿几块钱。现在刚从技校毕业那些小屁孩，来了不到一年就每月开两三千块，混个两三年就四五千，还少啊？”后来说是这个提案被天津的劳保局驳回，理由是如果丰田厂的待遇太好，不利于人才的正常交流，并且还可能扰乱社会安定（大概就是这个意思）。然后就是经济危机，鬼子就没再提出加薪的事。</div>

							
							
							
							
