江、宋的故事早在1998年就在北京传得人尽皆知。北京的出租车司机经常和客人兴致勃勃地聊起此事，打发路上堵车时的无聊时间。<br />
<br />
曾任中央电视台文艺部主任、多年执导春节联欢晚会的赵安，在2001年的一天邀请一些朋友到饭店宴聚，其中有宋祖英。席间宋祖英趁着酒大谈特谈她与江泽民的风流艳事，被赵安传了出去。后来赵和他的合作者、歌词作者张俊以有矛盾，张分两次向中央和国家机关主要部委领导人发了300多封匿名举报信，检举揭发赵安散布江泽民和宋祖英的绯闻。说是举报，但信中对江、宋关系描绘得非常详细具体，严格地说这叫传播，数百高官因此都知道了江宋乱的秘密。一开始大家都装不知道，所以张俊以两年来安然无恙。但后来不知何人把这捅到江那里，令江相当愤怒，甚至在政治局会议上大发雷霆以示清白。江下令立即处理，赵安以涉嫌受贿行贿罪被判10年，张俊以也以行贿罪判处6年徒刑。两人都进去了，江泽民以为缺口堵住了。<br />
<br />
后来，江泽民在十六大上搞特别动议，赖在军委主席的位置上不肯下台，激怒平民学者吕加平抖出江宋这段丑闻。江泽民抓人封口，于2004年2月23日把吕加平从家中抓走。<br />
<br />
第二天，网上出现最后通牒，警告江泽民，说他和宋祖英的每次幽会都有“专业版”的音像资料为证，如果吕加平第二天不被释放，光碟就会被向全国和全世界公布。在此之前，网上曾流传台湾某局长的情色光碟。此警告暗示江泽民有类似极其隐讳的情色把柄被人抓在手里。<br />
<br />
出乎所有人的意料，吕加平当天就被释放。光碟事件平息后，江泽民把吕加平抓到湖南，永远消音。<br />
<br />
宋祖英依旧呼风唤雨，成为国家一级演员，享受国务院“政府特殊津贴”，还成了全国政协委员、全国妇联执委、全国青联委员和中国音乐家协会理事。<br />
<br />
国家大剧院<br />
<br />
为了让宋祖英高兴，江泽民没少花国库的巨款。<br />
<br />
宋祖英一句话要到澳大利亚悉尼开个人演唱会，江泽民马上拨款数千万元人民币给海军，让宋祖英在澳大利亚和奥地利扬名。最奇怪的是，宋祖英是民歌手，她所演唱的民歌需要民族乐器伴奏，需要中国人的合唱团伴唱，但她在悉尼演唱会的伴唱都是连中国字都咬不清的外国人，伴奏用的都是西洋乐器。一个民歌演唱会台上只有她一个人是中国人，显得不中不西、不伦不类。2002年韩国世界杯开幕，主办方要求派名家去演唱，结果中国派出的是不够档次的宋祖英。尴尬的是，韩国给所有演唱的名星都付了很高的出场费，唯独没付给宋祖英一分钱。<br />
<br />
江泽民还曾经花千万元给宋祖英出首张精选DVD碟，于2002年农历新年前夕在全国上市。<br />
<br />
江泽民讨宋祖英欢心的最大一个礼物则是国家大剧院。<br />
<br />
2001 年12月13日新华社对外宣布，国家大剧院正式开工。国家大剧院位于北京人民大会堂西侧，占地11.89万平方米，总建筑面积14.95万平方米。大剧院主体工程总投资26.88亿元，外围工程由北京市投资8亿多元，建设工期4年，建成后正式营业尚需开办费3亿元。大剧院整个项目共需资金38亿元，差不多是过去15年希望工程累计接受海内外捐款总和的两倍，可以资助500万贫困学生受到教育。<br />
<br />
从立项开始，有关国家大剧院的纷争就从来没有停止过。<br />
<br />
无论从哪个角度，也没有一个专家认为有修建大剧院的必要，他们对此项目表示强烈反对和抵制。有学者明确指出，在下岗工人和民工的温饱都解决不了的前提下，完全没有必要花费三十多亿人民币修建这么一个浩大的消遣工程。况且担任设计的是法国机场设计师安德鲁，他从来没有设计歌剧院的经验，而他主持设计的法国戴高乐机场候机厅在2004年5月23日发生屋顶坍塌事故，造成6人死亡，多人受伤。法国巴黎检控官办公室5月29日表示，负责设计戴高乐机场新翼客运大楼的总建筑师安德鲁涉嫌在赢得中国北京国家大剧院投标过程中舞弊，法国当局2003年7月开始进行初步调查。<br />
<br />
建筑专家指出，从文化上考虑，大剧院给人的印象恰如外星人的巨大飞碟，降落在中南海的门口。什么文字也不用写，就能看出这是个天大的错误和笑话，与北京六朝古都的文化传统完全不般配。此外，从实用性考虑则问题更多。加拿大皇家建筑学会院士Michael Kirkland 称此设计将“建筑语言和基本科学规律都倒到沟里去了”：这是一个功能性非常强的建筑物，但设计人把它当作一个艺术品来做，大错特错，上面加了盖子，房子套房子，是在屋中打伞作茧自缚，结果需要高大空间的舞台上不去，要向地下挖六至八层楼，这是全世界建筑界有史以来最荒谬的大笑话。<br />
<br />
国际权威性建筑专业杂志英国 ARCHITECTURE REVIEW 1999年1月号更以社论“无法无天”（OUTRAGE）尖锐地批评法国建筑师设计的北京国家大剧院，直斥其为“完美的粪团”，与北京城中心和其它任何现有建筑完全不协调。<br />
<br />
对于大剧院的整个造型，有人说，从空中鸟瞰下来，大剧院就像“一口痰”。但江泽民却欢喜得不行。库恩在《江泽民传》中说，“江喜欢站在中南海的南端赏月并观赏南海映月的美景，从这里沿着水面望去，可以看见这座美不胜收的大剧院款款落成。” 事实上，世界著名建筑家贝聿铭所担心的正是从故宫能看到大剧院，因为这完全打破了古建筑群的和谐格局。<br />
<br />
大剧院最遭人诟病的是其外表看起来像一个“大坟包”，它的地下入口，就像是一条墓道。安德鲁安排了从水底下走入大剧院的通道（100米），所以观众得先钻下去走过一个水下隧道，再走上去。这象是在穿过坟墓的积水。<br />
<br />
民间有人说，中国的中心是北京，北京的中心是天安门广场，广场的中心是个坟包。号称讲究风水的中国人居然在中国最中心的地方造个坟，让故宫和大会堂都是“开门见坟”！还有比这不可思议的事情么？风水专家认为，“大坟堆”毁坏了北京的精妙布局，将先人循五行、五帝、四方四象之方位设计彻底破坏，倒阴为阳，使京城从此为阴尸气所罩。但有人说，这种格局有利于阴类在阳间猖獗。江来历特殊，据传与阴类有关，这或许是这个耗费巨资而又丑陋无用的愚蠢工程得到江泽民如此青睐的原因之一。</div>

							
							
							
							
