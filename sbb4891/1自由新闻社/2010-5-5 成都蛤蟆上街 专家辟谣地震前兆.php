																											<div id="postmessage_599603" class="t_msgfont">四川长宁突发地陷 7天现26个&quot;天坑&quot;<br />
2010-05-04 13:18:53　来源: 新华网(广州)<br />
<br />
<img src="http://cimg2.163.com/catchimg/20100504/7U6HS3BO_0.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
宜宾市长宁县硐底镇的巨坑<br />
<br />
新华网5月4日报道 昨日(5月3日)凌晨,随着“轰”的一声巨响,宜宾市长宁县硐底镇石垭村“大弯头”出现了一个巨大的坑,到了昨日中午,坑口直径扩展到约40米,深不见底。<br />
<br />
硐底镇从4月27日凌晨开始出现地层塌陷,这个巨大的“天坑”已是7天来的第26个坑。这些坑集中分布在该镇红旗村和石垭村,部分村民的房屋被扯开裂缝,5户村民无奈搬离。硐底镇党委副书记丁志成昨日表示,镇里已经启动自然灾害应急预案,“天坑”的形成原因正在调查之中。<br />
<br />
一声巨响 地面出现大坑<br />
<br />
巨响后是持续十多秒的“哗哗”声,那是地层塌陷后,沙石滑到坑底的声音,每一声都让人发抖、发冷。<br />
<br />
“坑是昨天凌晨3点过形成,当时一声巨响,我被惊醒后再也没睡着,被吓惨了。”回忆起凌晨的那声巨响,昨日,石垭村1组村民陈分模激动地说,先是 “轰”的一声,接着是持续十多秒钟的“哗哗”声,那是地层塌陷后,沙石滑到坑底的声音,每一声都响得让人发抖、发冷,就怕哪一声响起时,家一下就没了,人也没了。<br />
<br />
村民陈志毅家离坑约50米远,昨日凌晨他也被惊醒了,“那声音比打雷还响。”<br />
<br />
这已经不是第一个坑了,石垭村从4月29日开始,相继出现了4个坑。昨日,站在“大弯头”坡上这个深不见底的巨型“天坑”前,一晚没睡的陈分模双脚都在打抖。“小心点哟,很多地方已经垮成空心了。”正在现场牵警戒线的村干部邱建涛不停地招呼着前来看坑的村民。<br />
<br />
天坑密布 深不可测<br />
<br />
王利明将石头捆在一根100米长的绳上,将石头扔下坑。拴着石头的绳一直向下放去,放了约50米长都还没到底。<br />
<br />
除了石垭村,与之相邻的红旗村天坑更多,警戒标志随处可见。红旗村村主任王利明说,红旗村的坑从4月27日凌晨开始出现,到昨日中午,一共出现了 21个坑。<br />
<br />
在其中一个坑前,王利明将石头捆在一根100米长的绳上,将石头扔下坑,两秒钟后,只听哗哗两声响,王利明说,这是石头碰到了坑壁,将绳抖动几下, 有沙石落下,只听坑下传来很空荡的“哗哗”声,还伴着回音。拴着石头的绳一直如此向下放去,放了约50米长都还没到底。<br />
<br />
据了解,这26个“天坑”,最大的就是昨日凌晨出现在“大弯头”的巨型坑,直径约40米,深不见底。其他坑直径在数米到10米不等。几位村民说, “天坑”刚出现时,坑底传出明显的水流声,他们认为地下有阴河。村民不敢回家公路施工受影响。<br />
<br />
“这咋个还敢住人嘛,水泥砌的石梯都被扯坏了,房子也扯起裂缝了,万一有什么事,跑都跑不赢”。<br />
<br />
在红旗村的一个平坝上,两个蓝色的帐篷格外醒目,王利明说,帐篷是县政府提供的,目前已经有5户村民搬离了自己的家,住进了帐篷或自己的亲友家。<br />
<br />
韩廷富是红旗村6组的村民,指着自家房前被“天坑”扯坏的石梯,她说:“这咋个还敢住人嘛,水泥砌的石梯都被扯坏了,房子也扯起裂缝了,万一有什么事,跑都跑不赢。只好住在亲戚家。”同样搬离的还有村民张兴和,他每天晚上都在弟弟家住,但想着家里还有很多家具,白天都会回到自己的家里。张兴和房屋的几根支柱上都出现了裂纹。一些家里没被确定为危险区的村民也表示,已将小孩送到亲戚家去了,而且自己晚上也有不回家的计划了。村民还说,村里有些田地被天坑扯出了很粗的裂缝,还有水泥砌成的堡坎也被扯坏倒塌。<br />
<br />
正在这里进行龙头至巡场段公路改建的施工方也着急万分,在石垭村“大弯头”下的公路边出现了一个“天坑”,这个坑使刚建好的公路路基塌陷了约5平方米。施工方项目经理杨世叶说,“因为这里集中出现 天坑 ,为了安全,部分路段已经暂停施工,工程进度和路面行车受到了较大的影响。”<br />
<br />
没有人员伤亡 “天坑”成因正在调查<br />
<br />
红旗村和石垭村大量出现“天坑”一事已经成了当地的大新闻,其他乡村的村民专门坐着摩托车前来看“稀奇”,大家议论得最多的是“天坑”的成因。<br />
<br />
当地属喀斯特地貌 首次出现“天坑”<br />
<br />
一些村民说,距两村不远处有煤矿,采矿要往地层钻探,他们怀疑是有煤矿打通了地下河,造成水压过大,对两村的地下结构造成影响。<br />
<br />
据了解,当地属于喀斯特地貌,“天坑”有的在坡上,有的在田地里。村民们说,出现坑的地方多是沙石土质,这是硐底第一次出现“天坑”。他们听说过几十公里外的江安出现过“天坑”,但那边“天坑”的直径和深度与这边的“天坑”都不具可比性。<br />
<br />
启动应急预案 村干部通宵值班<br />
<br />
宜宾市长宁县硐底镇党委副书记丁志成说,“天坑”出现后,镇里便启动了应急预案,设置了安置点,同时向村民宣传应对知识,并有村干部 24小时轮流值班。丁志成证实,硐底以前确实没出现过这样的“天坑”。<br />
<br />
硐底镇国土所所长胡华表示,市、县国土部门的专家已经来看过,天坑形成的原因正在调查,他们已经在与省上的专家联系,希望得到技术支持。胡华表示, 此次出现天坑所幸还没有人员伤亡。<br />
<br />
昨晚,长宁县国土局局长雷彬表示,事发后,国土部门在危险地段布置了警戒线,并向上级部门报告,“天坑”形成的原因还有待进一步调查。</div>

							
							
							
							
