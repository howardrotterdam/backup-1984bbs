四川在线5月5日报道&nbsp;&nbsp;万余只蟾蜍排成200米的长队，有组织有纪律地“聚众游街”、“霸道”横行……昨日上午，这个壮观的一幕出现在十陵镇太平村六组。而在昨日上午11时左右，青白江城厢镇杨家店附近，也出现了数万只小蛤蟆出洞聚会的场面。附近居民惊呼：“地震又要来了。”专家表示，这种情况属这个季节蟾蜍由蝌蚪成蛙过程中的正常迁移现象，与地震前兆无关。<br />
<br />
蟾蜍“游街”居民封门 <br />
<br />
昨日上午7时许，家住十陵镇太平村六组的张女士，在自家田地的小路上，惊讶地发现一群排着队的小蟾蜍正“途经”她家门口。“这些蟾蜍只有蚕豆大，匆匆忙忙地往前爬。”张女士说，在好奇心的驱使下，她跟随着蟾蜍队伍往前走，“这个队伍起码有200米长。”<br />
<br />
由此，张女士断定，蟾蜍队伍肯定是“有组织”、“有原由”才集体行动的，“该不会又要地震了吧?”张女士揣测。<br />
<br />
昨日上午11时许，记者来到十陵镇太平村。在蟾蜍“游街”的现场，记者见到这支队伍确实有200余米长，宽度10厘米到50厘米，蟾蜍密密麻麻地排在一起。记者目测蟾蜍数量约有万余只，丝毫不顾忌旁边围观的村民，接踵摩肩地前行……“不光是太平村六组有蟾蜍聚众游街，我们八组也有。”家住太平村八组的周大爷说，村民听说蟾蜍背部有毒，昨日一上午时间都在家中对家具逐一进行密封。用砖头封堵门缝、用报纸遮窗户、用米板盖上水缸……“生怕有掉队的蟾蜍跑进自己家里。”周大爷表示。<br />
<br />
专家：这不是地震前兆 <br />
<br />
 <br />
<br />
“我在这里住了10年了，从来没看到过这么多蟾蜍。”张女士很不解地说，“我怀疑是不是地震又要来了。‘5・12’地震的时候也没看到过这么多蟾蜍啊!”“这些蟾蜍很有可能是从附近一个池塘里爬出来的。”一名中年男子说，“它们也不光是游街，我家的院坝里也有好多。”<br />
<br />
昨日下午，记者采访了成都动物学家赵力，他表示近日天气炎热，小蟾蜍在清晨和傍晚排队转移实际是在寻找更加凉爽透气的环境，“它们像人一样，在寻找避暑纳凉的新居所。”<br />
<br />
四川省地震局副局长王力表示，这种情况属这个季节蟾蜍由蝌蚪成蛙过程中的正常迁移现象，与地震前兆无关。<br />
