2010年03月18日 07:19来源:人民网<br />
人民网・天津视窗3月17日电：看着温家宝总理的答记者问，69岁的李溥老人喃喃自语：“他从小就是有大志的人，能成为国家领导人不是偶然的。”<br />
<br />
李溥老人是温总理的“发小”，小时候和温总理住邻居，同样在达摩庵胡同度过了多年蜗居生活。<br />
<br />
李溥也是温总理的校友，同样在天津南开中学度过了6年时光。<br />
<br />
直到现在，他们仍保持联络。<br />
<br />
回忆起与温总理交往的点点滴滴，从李溥老人的叙述中，温总理温和、亲切的形象离我们更近了。<br />
<br />
右手写字左手秀球<br />
<br />
走在南开中学的校园里，中学时代的温总理在李溥老师的讲述中也渐渐清晰…。<br />
<br />
1954年虚岁12岁的温家宝考入了南开中学(时称十五中)，1960年高三毕业；李溥也是温总理的校友，1956年进入南开中学，1962年高三毕业，同样在天津南开中学度过了6年时光。<br />
<br />
砖红色的范孙楼，依然保持着古朴典雅的韵味，棕色的大门刷了新漆，南开校父严范孙先生的汉白玉塑像正对着大门入口。二楼正对着楼梯的一间教室如今已经变成了阅览室。<br />
<br />
“以前这里曾是温家宝总理所在的高三1班的教室。”<br />
<br />
温总理上学时曾住过的宿舍――204，即二排4室还在，不过如今已变成了国际部外国留学生公寓。安有暖气，不用再下课后自己再生炉子了。<br />
<br />
李溥比温总理大一岁，但温家宝5岁上小学，李溥考入南开中学的时候，温家宝已经是初三年级的学生了。<br />
<br />
李溥回忆，他不偏科，每门课的成绩都很好。当时每班好象是四十多人，当年温家宝常坐在中间靠前的座位置。”<br />
<br />
温家宝在操场上打篮球的身影留给李溥的印象最深，“他是南开中学篮球队校队少年队的成员，还在天津市比赛中拿过名次。”<br />
<br />
“他右手写字，左手秀球(投篮)，温家宝打球的样子很潇洒，他左手投球的特点给人印象深刻。”李溥老师不无幽默地说：“看来温家宝总理左右脑都很发达，所以，智慧超人。”<br />
<br />
接着，李溥老师解释说：“其实，温家宝最初喜欢踢足球，但有一次不小心，他把范孙楼后的一块玻璃打破了，惹了祸也不告诉家里。学校有严格的赔偿制度，当时一块玻璃要赔偿3毛钱。”总务处老师多次在他教室门上的小缝里贴纸条，催促他偿还玻璃钱。当时老百姓生活水平很低，温家宝只有从妈妈每天给5分钱早点钱里攒，攒了好一阵子，才凑够了赔玻璃的钱。从此，温家宝不踢足球了，改打篮球。”<br />
<br />
“五十年代时，尤其放暑假，天津不少青少年在胡同家门口，都流行成立小篮球(实为小皮球)代表队。我给我们胡同(达摩庵前胡同)的代表队起名 “达联”，先找来一块不足一米的长方形木板，用铁丝做个篮筐，再编个网子，然后把篮板钉在胡同的砖墙上，立刻引来胡同里不少青少年玩小皮球，也就是从那时起，温家宝微笑着用左手跳起准确秀篮的动作，给我留下很深刻的印象。”<br />
<br />
“暑假里，在家门口除了搞一些小皮球、乒乓球、跳高体育活动外，还有吹口琴、横笛的音乐活动。我家北邻的宋明荷学长是南开中学1955届毕业生，年长我们五、六岁，他擅长写作与唱歌，温家宝曾向他学会了唱《傲包相会》。”<br />
<br />
李溥老师说：“后来在北京他的家里，他曾向李溥回忆起南开教音乐的姜韵泉老师，每次在音乐教室上音乐课之前，总是给同学们弹奏一遍钢琴曲《土耳其进行曲》，久而久之，那优美铿锵的旋律令他终生难忘，以至八十年代他出访欧洲某国时，他能一下子听出了所演奏的迎宾曲是《土耳其进行曲》时，令该国元首十分惊呀、佩服中国领导人的音乐修养。”<br />
<br />
