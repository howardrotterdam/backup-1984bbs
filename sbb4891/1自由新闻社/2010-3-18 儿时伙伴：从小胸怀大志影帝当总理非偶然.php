<br />
“我和他接触中，听他说的最多的一句话是‘靠自己’。”李溥老师说。<br />
<br />
说到这里，李溥有所感悟地向记者透露了一个从未和任何人说过的细节――“以前，温总理家的走廊尽头墙上悬挂着两个墨笔字：‘高远’，是志存高远之意；近几年，我再去时，主人家墙上的‘高远’两个字悄然改变了一个字，变成了‘高洁’两个字。虽然仅一字之差，却意味深远，耐人寻味。<br />
<br />
“温家宝能成为继周恩来后从南开中学走出来的又一位国家总理，这决不是偶然的。”李溥老师认为“首先他从小受到受到良好的家庭教育。他的祖父是位民办教育家、他的父母是中学和小学老师。在家庭的熏陶和父母庭训下，他健康成长。<br />
<br />
温总理在前不久与网民交谈时，还动情地感念年逾九旬已患脑溢血重症的母亲从小对自己的教诲。<br />
<br />
少年时的温家宝就是温和、亲切的性格，从不和人争吵，从不胡打乱闹，不说赃字；温文尔雅，待人谦和，对师长很有礼貌。<br />
<br />
他的母亲从小教育他待人要真诚，与人为善，助人为乐，并身体力行。因此，他的母亲在全胡同内左邻右舍间威信很高，人们都亲切恭敬地称她‘杨老师’。<br />
<br />
八十年代初，杨老师退休后还热心居委会工作，杨老师尊称我母亲为‘李奶奶’，她一有空就过来看望‘李奶奶’，并把温家宝从北京捎来孝敬父母的一些补品转送给我母亲‘李奶奶’享用。”<br />
<br />
李溥说：“在他家里三个孩子中温家宝排行老大，宽厚仁爱，颇有作兄长的风范；他生活简朴，上中学时他穿的鞋子基本都是杨老师亲手做的。”<br />
<br />
李溥还记得杨老师在天津时向他说过的一件事――<br />
<br />
“有一次下大雨后，家宝放学是光着脚趟水回到家的，我很纳闷，明明有新做的鞋，家宝却不穿！我很快发现，家宝把新做得鞋子用布包裹起来抱在身上，他舍不得穿，怕新鞋子进了水，就自己趟着水回来了。”<br />
<br />
“2003年，我有机会去甘肃采访老校友时，特意到了温家宝当年工作过和生活过的甘肃酒泉，寻访在那里温家宝曾经住过的平房里外间简易宿舍，寻访了力学队办公楼，还寻访了兰州市内的温家宝工作过的甘肃省地质局。走访了他曾经的同事，知道了温家宝的很多‘故事’。”<br />
<br />
“在酒泉，有个对温家宝感恩一辈子的女同志，她是温家宝在地质队时一位司机的孩子，因家里收入低，生活困难，读不起书。温家宝夫妇知道了二话没说，每个月从工资里拿出一部分钱，资助这个女孩读书，一直资助到女孩参加工作。此事，我到北京去看望杨老师并叙说温家宝在甘肃曾多年资助别人的往事时，杨老师听后也有些诧异，因从未听他说起过。”<br />
<br />
八十年代后期，温家宝已在中央工作多年了。一天，温家宝的母亲杨老师若有所悟地向李溥说起当年温家宝说过的一句话：“有一天，家宝在学校听了一场报告后，回到家里，他很坚定地跟我：‘妈妈，长大以后我要为国家做大事！’”<br />
<br />
永远的学生<br />
<br />
令李溥老师印象最深的是，“温总理是个有情有义的人，他虽然‘官’越做越大，却不忘记从前的老师和同学。”<br />
<br />
“1968年，已读完研究生的温家宝从北京地质学院分配到甘肃省地质局地质力学队，从事野外探矿工作，一干就是十一年。<br />
<br />
”“刚好也是这一年，我大学毕业后从天津被分配到塞外宣化部队农场劳动，接受工农兵‘再教育’。从此，我们彻底失去联系。1978年，我从沧州地区的一所农村中学，调回天津南开中学母校任教。”<br />
<br />
李溥老师有些兴奋地说道：“至今，我还清楚地记得，那是1983年初秋的一天晚上，我正在灯下备课，”<br />
<br />
忽然听见院子里有人喊我的名字‘李溥！’，我立刻推门出去，只见在皎洁的月光下站着一个既陌生而又熟悉的人，神采奕奕的，但我很快便认出了是温家宝。<br />
<br />
他的突然出现，真让我喜出望外！<br />
<br />
我忙请他到我的9平米陋室里坐，他进屋后在我的下n床边坐下。他那天穿了一件笔挺的蓝色中山装，非常精神。<br />
<br />
他微笑着告诉我，不久前他从甘肃省地质厅调到北京地质部做副部长了。<br />
<br />
我自然为他感到高兴。他还关切地询问起我的工作情况与南开母校的情况。<br />
<br />
温家宝的到来，一下子使小院子热闹起来，我的母亲、父亲、弟弟、妹妹都过来跟温家宝说话，温家宝还像当年那样，亲切地与大家交谈。”<br />
<br />
“从南开中学毕业以后，温家宝很多年没有回到天津，那天他从北京回天津来看望父母，而看望的第一个南开校友就是我。从此我和温家宝恢复了联系，并常去温家宝北京的家中看望。”李溥不无自豪地说。<br />
<br />
“温家宝总是念念不忘自己的恩师。每次去北京和他交谈时，他总是打听学校情况和几位老师的身体情况，并嘱咐我一定代他向陈东生老师、孙养林老师、乔慈忠老师、安同沛老师、刘铎老师、张思聪老师等问好。”<br />
<br />
2003年11月下旬，令温家宝敬重的南开中学的生物老师孙养林先生病重，住进了二中心医院。<br />
<br />
李溥立即打电话向已出任国务院总理八个多月，日理万机的温家宝报告这一情况。<br />
<br />
李溥说： “他让我代替他去医院看望孙先生，并要给孙先生送上一束鲜花儿，写上‘祝您早日康复’。我问‘落款怎么写？’他说，只写‘您的学生敬献’，提醒我不要写他的名字。<br />
<br />
我当然照办了，来到第二中心医院，带来了学生温家宝送给孙养林先生的鲜花。<br />
<br />
没过几天，11月26号，孙养林先生的病情恶化，我又及时打电话报告给北京的温家宝。他听后，沉默了一会儿，对我说，‘如果孙先生不幸去世，你就替我买个花篮。”<br />
<br />
11月27日，经抢救无效，德高望重，深受学生爱戴的孙养林先生不幸与世长辞。<br />
<br />
在北仓送别孙养林先生的灵堂里，摆有不少花圈，但是一个写着：“恩师孙养林先生千古――学生温家宝敬挽”的花圈格外引人注目。</div>

							
							
							
							
