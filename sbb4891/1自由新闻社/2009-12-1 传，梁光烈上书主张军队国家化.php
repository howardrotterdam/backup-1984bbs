																											<div id="postmessage_349957" class="t_msgfont">国防部长梁光烈上书主张军队国家化<br />
<br />
中共十七届四中全会以来，军政高层表面上波澜不惊，实际上暗涛汹涌，角力不断。一个突出的例证就是：现任国务委员、国防部长梁光烈最近公开上书中央政治局和国务院各相关部委、办、局，要求全面“改革”军队体制编制，尽快实现军队国家化，公开否定共产党建军的纲领和原则――党指挥枪。<br />
这份公开信的名义是“对当前我国国防体系存在问题的思考和建议”，实际上是全面否定了中共现今的全部建军理论、原则和方针。梁光烈认为，全世界的军队都是由国防部领导、组织指挥的，惟独中国军队不是。这样的军队是不能打仗的。中国军队名义上受四大总部和两个军委的领导，实际上是两块牌子一帮人。中央军委的现状是，江泽民、胡锦涛根本不懂军队，郭伯雄、徐才厚两个人说了算。全军全国大到战略、人事，小到日常运转，只由郭、徐两人说了算，怎么能搞得好！<br />
为了从根本上改变中国军队的现状，梁光烈的白皮书提出需分四个步骤：<br />
（一）全面整编改变国防动员体制。梁说，现在全军军以上单位编制，31个省军区就占去了一多半，还有近十万人的边防部队。改变军队编制体制的问题，首先应该撤销各省（直辖市）省军区、军分区、人武部编制，成立省、市、县兵役局，负责战时征兵、平时预备役民兵训练等任务。全国省军区、军分区的部队直接归国防部长指挥。全国国防动员系统包括近十万人的边防部队，也应该直接接受国防部长指挥。国防部仿效前苏军的体制实现实体化运行。<br />
（二）效仿美军三军联席会议制度，撤销中国共产党中央军事委员会和中华人民共和国军事委员会，撤销四大总部、七大军区、二炮和武警编制，成立陆军部、海军部和空军部。<br />
（三）大军区编制全部撤消后，整编成若干个可独立完成军事任务的合成军及特种作战师、旅，由陆军部直接指挥调度。海军、空军和二炮部队配合遂行军事作战任务。<br />
（四）武警部队撤销后，除压缩部分兵员，大部分改制公安民警，划归地方政府管理指挥。<br />
这份白皮书的主张，如果真能实现，不仅会使一直盼望并习惯于握有更大权力的梁光烈变成真正的“军头”，而且更重要的是，从根本上否定了由执政党指挥军队的根本原则，瓦解了共产党在军队的权力基础，进而从军权到政权，真正实现了军队国家化。梁光烈的白皮书虽然迄今为止并未得到中共中央和国务院肯定与否的答复，但这份白皮书不胫而走、越传越广的事实，已经不证自证地说明了有关各方的立场态度。<br />
党和军队的关系，谁指挥谁，一直是中国政治体制的核心问题，不公开的争论从未停歇过。从邓小平、江泽民、胡锦涛三任军委主席都强调和延续党指挥枪的传统，因为如果谁否定了这项根本原则，就有可能被认为是中国共产党的叛徒。客观地说，梁光烈提出军队国家化的四个步骤也并非全无道理，但此议出自他口，总给人某种怪异和诡秘的感觉！据了结高层动态的人士透露，在离中共十八大还有三年的时间，身为国务委员和国防部长的梁光烈，敢冒巨大的政治风险打出决定身家性命的“胜负手”，其实是有他不得不为之的缘由：<br />
其一，迫于外界形势。不久前结束的中共十七届四中全会上，出乎外界预料，习近平并没有重复当年胡锦涛接班的套路，提前进入军委第一副主席这一关键位置，但党内对军委接班人选的竞争一刻也没有停止过。前不久，中央已明确在1950后的大军区领导干部中推荐和遴选新的中央军委成员，为十八大前中央军委增补改选打响了前哨战。按照这个态势，年龄已近七旬的梁光烈，如果没有特殊原因和特殊贡献，十八大之后是不可能留任的，甚至在十八大之前随时有可能“裸退”。至此山穷水覆之时，投机赌博成性的梁光烈认准时机，调转炮口，将自己塑造成改革党军体制的实际推手，以求成为十八大之后实际掌握军权的“周勃”。应该说，这的确不失为一步好棋。<br />
其二，出于个性本能。细看一下梁光烈的履历便不难发现，行武出身的梁光烈，虽然文化程度不高，但有超强的个人奋斗和攀附各种政治势力的投机能力，所以能在军队一路快跑，直至做到大军区司令、国防部长。梁光烈的个性表面简单粗暴，故有“梁大炮”之称，实际上极具心机，敢赌敢赢 ，从不知道满足，无论是名、利、权、钱、色，无所不求其极！十七大后，前军委副主席、国防部长曹刚川退休，中央没有让梁光烈接替曹刚川军委副主席的位置，梁光烈对此一直认为是奇耻大辱，耿耿于怀。尤其是对现在主持军委日常工作的郭伯雄、徐才厚两位副主席，梁光烈更是从心眼里瞧不起，他的这种心态在军委及各大军区领导干部中几乎是公开的秘密。梁光烈平时喜欢说“我的眼里最揉不得沙子”，正是他这种性格的自我表白。<br />
梁光烈何许人也？梁是全军最有钱的人。据了解内幕的人透露，梁的身家应有几十亿元，仅2008年金融风暴期间，梁的子女在股票证券市场上的投机损失就达8~9亿元。在梁看来，没有权力就保不住金钱，所以一切行动均以维护权力为核心。十七大以后，梁光烈对于将其从握有实权的总参谋长调为并无实权的国防部长是非常不满的。但自知自己劣迹斑斑，也只能忍气吞声，暗中使坏。<br />
