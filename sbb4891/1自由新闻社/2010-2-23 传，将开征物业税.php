昨晚和一个朋友的朋友的朋友喝酒，就是那种一大帮人胡吃海喝，中间和一个坐我旁边的哥们聊起来，他是北京税务行业工作的，哪行哪业我就不说了，别给人惹麻烦，他多喝了几杯，和我说了几句掏心窝子的话，听得我毛骨悚然。<br />
