													<h2>传，北京欲染指吴宇森新片《飞虎群英》</h2>
						
						
																											<div id="postmessage_463635" class="t_msgfont">好莱坞著名华裔导演吴宇森已秘密与台湾方面接洽，筹划合作开拍一部以二战期间美国空军飞虎队与国民党空军联合抗日为历史背景的史诗电影。有消息称，中共当局藉机拉拢导演，欲插手该片，染红美国飞虎队。此事引起台湾军方及舆论界狂批，台湾军方更严正指出，“历史不容许篡改”。 <br />
<br />
据苹果日报报导，吴宇森早前与台湾国防部秘密接洽，希望合作开拍一部暂名为《飞虎群英》、反映二战时期，美国空军志愿队（俗称飞虎队）在中国与国民党空军共同抗击日本侵略军的历史影片，并获台湾军方大力支持。台湾国防部长高华柱认为这部电影有助于台军历史精神传承，要求军方全力配合，负责出人出力。<br />
<br />
有消息人士指出，目前台湾军方除了向吴宇森提供飞虎队成员日记、各级徽章等多项史料作为编剧和道具制作参考外，也同意提供基地搭建战争场景，并支持所需的兵力演员需求。<br />
<br />
据报导，吴宇森已在好莱坞筹资1.5亿美元。初步计划电影空战动画在美国制作，人物和室内剧情选在台湾拍摄，飞虎队基地的取景则包括云南昆明。因影片要在大陆拍摄，中共当局对该片大表热情，在去年7月吴宇森在大陆做前期准备工作时给予“热情”接待。<br />
<br />
报导指，中共当局频频向导演吴宇森示好，希望吴导在片中加入中共协同抗战的元素，甚至想争取成为影片的“主要成份”。<br />
<br />
中共欲染红该片，篡改国军与美军共同抗战的历史引起台湾各界关注。<br />
<br />
台湾军方发言人虞思祖指出，飞虎队的历史“是中华民国空军与美国合作共同抗日，这段历史不容许篡改！”<br />
<br />
台湾舆论界狂批中共篡改历史。有媒体引述台湾军方官员表示，难以接受中共的做法，强调中华民国空军这段辉煌战史，近年来已全数被中共收编，纳入“中国空军”的战绩，并遭到扭曲，期望吴宇森的影片，能还原历史真相，给国军一个公道。<br />
<br />
飞虎队背景<br />
<br />
美国飞虎队正式名称为中华民国空军美籍志愿大队（American Volunteer Group，简称 AVG），是二战期间由美国志愿者组成的空军部队，在中国、缅甸等地参加反抗日本的战争。飞虎队由美国退休飞行教官陈纳德（Claire Lee Chennault图）创立。<br />
<br />
陈纳德于1937年到中国，获蒋介石委任为空军顾问。1940年他开始组建飞虎队，回美国宣传并获罗斯福总统（Franklin D. Roosevelt）支持，批准美军现役空军加入，并拔给129架战机。飞虎队战机头部统一绘上鲨鱼嘴队标，机身有张开翅膀扑向目标的老虎。<br />
<br />
从1941年12月20日首场对日空战，到1942年7月4日解散，飞虎队共参加对日空战数百次，共击毁日机299架，歼灭了大量日军地面部队；而飞虎队也损失战机73架，牺牲26名飞行员。<br />
<br />
1942年7月4日飞虎队被并入美国现役空军, 不久改编为第十四航空队，由陈纳德将军担任司令员，继续参加援缅战争,并开辟了著名的驼峰航线。<br />
<br />
航线全长500英里，地势海拔均在4,500～5,500米上下，最高海拔达7,000米，山峰起伏连绵，犹如骆驼的峰背，故而得名“驼峰航线”。“驼峰航线”是世界战争空运史上持续时间最长、条件最艰苦、付出代价最大的一次悲壮的空运。<br />
<br />
在长达3年的艰苦飞行中，“驼峰航线”向中国战场运送了80万吨战略物资、人员33,477人。很多原飞虎队成员参与了“驼峰航线”飞行。 据战后美国官方的数据：美国空军在“驼峰航线”上一共损失飞机468架，牺牲和失踪飞行员和机组人员共计1,579人。当时的中国政府一方损失飞机46架、牺牲飞行员168人。<br />
<br />
1946年，美国《时代》杂志第一期记载:“至战争结束，在长520英里、宽50英里的航线上，飞机的残骸七零八落地散落在陡峭的山崖上，而被人们称为‘铝谷’。在天气晴朗时，飞行员可以把这些闪闪发光的铝片堆，作为航行的地标。”这就是中美飞行人员用鲜血和生命铺设而成的“航标”。</div>

							
							
							
							
