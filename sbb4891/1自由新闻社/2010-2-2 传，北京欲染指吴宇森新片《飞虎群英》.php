凭借《赤壁》创造了华语电影票房的又一个巅峰后，著名导演吴宇森的目光又瞄准了云南。7月3日下午，吴宇森出现在云南省委宣传部在北京召开的“云南影视剧创作重大题材规划座谈会”上。吴宇森和云南省委宣传部当场拍板，耗资1亿美元，由其执导云南题材电影《飞虎群英》。吴宇森在现场接受媒体记者专访时说，中国电影要走向世界，既要有中国精神，也要有世界眼光，《赤壁》表现的是中国的精神，《飞虎群英》也将表现中国和云南的精神。<br />
<br />
两年前就盯上飞虎队题材<br />
<br />
早在两年前，吴宇森就开始关注发生在云南的飞虎队故事题材了，目前，拍摄工作已经基本准备就绪，剧本由《士兵突击》、《我的团长我的团》编剧兰小龙撰写，还有一个月剧本就可以定稿。他觉得，这个题材非常适合在现在这个时候拍。<br />
<br />
他说，这是一个非常重要的作品。这部以云南为题材的电影，强调中美友谊，强调飞虎队和云南人民对抗战的贡献，“云南有深厚的文化，有崇高的人文精神和牺牲精神，之前，全世界都知道云南是非常美丽的地方，可对云南故事和历史知道得不多，通过飞虎队可以让更多的人了解和喜爱云南精神。”<br />
<br />
据吴宇森介绍，整部电影将集中反映抗日战争时期，中美两国飞行员以及游击队、军民作战的故事。吴宇森说，陈纳德将军招募美国飞行员和机械师成立飞虎队协助中国空军对日作战，他们的坚强意志、正义感，都将在电影中体现出来，而这一题材也正好吻合他一直倡导的“侠义精神”。<br />
<br />
准备重建一个飞虎队司令部<br />
<br />
虽然影片的拍摄地点尚未完全确定，但在昆明将有很多的戏份。影片将在昆明、湖南和河南拍摄三场大战，希望拍成中国电影史上最宏大的空战场面，其中一场飞虎队飞机坠毁的戏非常精彩。<br />
<br />
为拍好这部巨片，吴宇森做了长期的准备工作，收集了许多资料，看了很多有关的纪录片，还专程到昆明、丽江看外景。吴宇森说，他找到当年飞虎队的司令部，房子还在，只是已经不完整，他准备重建一个司令部，而过去的战场将用科技手段去表现。<br />
<br />
中美两国影星将联袂出演<br />
<br />
据了解，这部电影投资庞大、场面宏大，拍摄难度很高，除通过电脑特技制作当年的飞机外，还要按照当年的飞机原样量身制作实战飞机，中美双方将在技术、人才方面进行全面合作。<br />
<br />
吴宇森说，有云南省委、省政府发展文化产业的坚强决心以及良好的政策环境，他相信能把这部电影拍摄好。吴宇森表示，他很欣赏和相信中国大陆的电影人才，希望能够在云南找到强大的国际性合作工作组，让云南的工作组参与拍摄，了解好莱坞电影拍摄方式。同时，他将邀请好莱坞影星和中国影星联袂出演，由好莱坞投资并向全球发行放映。<br />
<br />
该片将于今年12月开拍，拍摄所用时间大概在半年以上。吴宇森透露，今年年底开始拍摄电影特技部分。 （本报记者 杨蓉 文）</div>

							
							
							
							
