													<h2>公盟缴税被当局拒绝 银行账户被冻结</h2>
						
						
																											<div id="postmessage_199045" class="t_msgfont"><a href="http://xuzhiyong.net/2009/08/11/30.htm" target="_blank">http://xuzhiyong.net/2009/08/11/30.htm</a><br />
<br />
公盟文件<br />
<br />
公盟关于履行税务行政处罚决定情况的公告<br />
<br />
公盟缴税被拒绝<br />
<br />
8月3日，公盟发布《关于同意接受社会捐款用于履行税务行政处罚决定的公告》，积极筹备资金，准备缴纳两税罚款（捐款账户详情公盟将在近日公布）。<br />
<br />
8月5日，公盟账户（开户名称：北京公盟咨询有限责任公司，开户银行：中国光大银行北京首体支行，帐号：35080188000016762）被北京市公安局冻结。<br />
<br />
公盟已于8月10日上午向北京市公安局致送了请求解冻账户协助办理缴纳税款和罚款的书面文件。8月10日下午，分别到地税和国税申请缴纳罚款。但皆被拒绝。现公告近日公盟缴税过程详情如下：<br />
<br />
地税局：无法人代表签字不能缴税<br />
<br />
2009年8月6日下午，公盟代理律师黎雄兵来到位于朝阳区裕民路12号院的北京市地方税务局第二稽查局，找到第一稽查科的张旭科长，要求开出税收缴款书，履行此前由张旭、曹栋斌、刘朝晖、白俊等税务官对公盟作出的税务处理决定和处罚决定，缴纳税款和罚款。（1、2009年7月17日，北京市地方税务局第二稽查局二稽税稽处[2009]47号《税务处理决定书》，责令公盟补缴营业税、城建税、印花税合计60995.13元；2、2009年7月30 日，北京市地方税务局第二稽查局二稽税稽罚[2009]56号《税务行政处罚决定书》，处罚公盟缴纳营业税、城建税、印花税所偷税款五倍的罚款 304975.65元。）<br />
<br />
张旭税务官告知，执行的问题归执行科管，建议公盟拿着税务处理文书去执行科开税单，然后凭税单去银行缴款，并告知黎雄兵律师第二天即可以去执行科开具税单。<br />
<br />
2009年8月7日下午，黎雄兵律师再次来到北京市地方税务局第二稽查局执行科要求开具税单以便到银行缴税。一位自称姓安的副局长和姓郭的税务官接待了黎雄兵律师，他们告知黎雄兵要求有公盟的委托授权书，才能办理。黎雄兵律师说，7月24日参加处罚听证时，已经提交了委托授权书，听证主持人白俊的手中存有该授权委托书。安副局长和郭姓税务官说，该授权委托书是听证授权，不是缴纳税款的授权，因而黎律师不可以代办公司的缴税工作。<br />
<br />
2009年8月10日下午2：30，黎雄兵律师与公盟研究员王功权先生带着盖有公盟公司印章的代为办理缴纳税款及罚款事宜的专项委托书，再次来到位于北京市地方税务局第二稽查局办公大楼417房间的执行科，请求开出二稽税稽处[2009]47号《税务处理决定书》和二稽税稽罚[2009]56号《税务行政处罚决定书》所对应的缴款税单，以便到银行办理缴款手续。<br />
<br />
执行科的王元锋科长说要请示领导后才能办理，让黎雄兵律师与王功权研究员稍等。不久，王元锋科长回来，告知黎雄兵律师所持的授权委托书不符合要求，不能办理开具税票的事项。理由是，黎雄兵律师所持的盖有公盟公司印章的专项委托书上没有法人代表许志永的亲笔签字。<br />
<br />
黎雄兵律师对王元锋科长说，公盟的法人代表许志永现已被北京市公安局以涉税问题羁押于北京市第一看守所，律师未获批准会见，目前无法在授权委托书上签字，盖有公司印章的授权委托书是具有法律效力的，要求依法予以办理准许缴纳税款及罚款的手续，开具税单。<br />
<br />
这时，同在王元锋科长办公室的执行科的夏女士、范女士两位税务官也参与进来强调说，要求授权委托书上有法人代表的亲笔签字这是税务部门的规定，“你们有异议的话，我们也没办法”。<br />
<br />
于是，黎雄兵律师与王功权研究员来到位于三楼的安副局长办公室，请求协调执行科给予开出缴纳罚款的税单。安副局长告知，该事归陈姓副局长分管，或者是一把手郭局长管，建议黎、王二位去二楼办公室请求帮助联系陈副局长和郭局长，由陈副局长和郭局长处理。<br />
<br />
黎雄兵、王功权下到二楼稽查局办公室，办公室主任苏雷和科员李楠楠接待了黎雄兵、王功权，说郭局长正在开会，陈副局长休假了，均不能联系到。他们只能答应帮助转告该事情。<br />
<br />
黎雄兵律师问苏雷，什么时候可以给予答复，苏雷说，这个他也说不准，没法给出准确时间。黎雄兵律师和王功权研究员担心，如果不能及时联系上陈、郭二位局长，公盟将被迫按照《税务处理决定书》和《税务行政处罚决定书》所确定的每日百分之三的利率缴纳逾期履行的罚款，每天将额外支付一万多元的滞纳金。<br />
<br />
苏雷回答说，“这样的话，我们也没有办法”。黎、王二位据理力争，最后稽查局的多名工作人员连同两名保安员强行将黎雄兵律师、王功权研究员赶出了朝阳区裕民路12号院北京市地方税务局第二稽查局的办公大楼。<br />
<br />
国税局：税务处理决定有误不宜执行<br />
<br />
从朝阳区裕民路12号院地税局办公大楼被驱逐出来后，黎雄兵律师、王功权研究员又赶往西城区车公庄大街的北京市国家税务局稽查局，请求开具国税缴款税单，履行京国税稽处[2009]5号《税务处理决定书》。该决定书责令公盟补缴所得税187424.87元。<br />
<br />
张允安税务官接待了黎雄兵和王功权，告知暂时不能补缴税款。理由是：原定五倍罚款的处罚决定还没有做出来，经过7月30日上午的听证他们认为公盟的申辩是有道理的，打算改变决定降低处罚额度；同时，国税稽处[2009]5号《税务处理决定书》所确定的补缴所得税187424.87元也需一同变更，原来送达给公盟的京国税稽处[2009]5号《税务处理决定书》暂不适宜执行。<br />
<br />
黎雄兵律师和王功权研究员请求张允安税务官将上述答复出具书面文件给公盟，担心依据现有的京国税稽处[2009]5号《税务处理决定书》，公盟将承受额外的滞纳金。张允安税务官拒绝了黎、王二位的请求。<br />
<br />
