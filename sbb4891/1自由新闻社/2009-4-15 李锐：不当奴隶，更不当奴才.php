<strong>作 者 简 介</strong><br />
<br />
现 年 92 岁 丶 曾 任 毛 泽 东 秘 书 的 李 锐 素 以 敢 言 着 称 ， 也 因 敢 言 而 先 後 被 毛 泽 东 重 用 丶 弃 用 ， 近 年 被 称 为 体 制 内 的 自 由 派 。 2006 年 ， 李 锐 曾 致 函 中 共 总 书 记 胡 锦 涛 ， 希 望 中 共 停 止 以 党 代 法 以 重 建 信 誉 。 2007 年 中 共 十 七 大 召 开 前 ， 李 锐 在 《 炎 黄 春 秋 》 发 表 文 章 ， 指 中 共 若 不 落 实 民 主 ， 便 会 危 机 丛 生 。 <br />
<br />
1958 年 初 ， 李 锐 任 水 利 电 力 部 部 长 助 理 兼 水 电 建 设 总 局 局 长 时 ， 曾 向 毛 泽 东 陈 述 三 峡 工 程 引 发 的 问 题 并 提 交 文 章 ， 获 毛 赏 识 ， 毛 还 说 ： 「 我 们 要 有 这 样 的 秀 才 。 」 李 就 当 了 毛 的 秘 书 。 1959 年 ， 庐 山 会 议 召 开 ， 不 少 人 不 敢 公 然 反 对 毛 领 导 的 「 大 跃 进 」 ， 但 李 大 胆 直 说 ， 其 後 被 开 除 党 籍 ， 文 革 期 间 被 关 在 秦 城 监 狱 8 年 。 至 1979 年 ， 李 锐 获 平 反 ， 任 电 力 工 业 部 副 部 长 ， 其 後 出 任 中 共 中 央 组 织 部 副 部 长 。</div>

							
							
							
							
