																											<div id="postmessage_75055" class="t_msgfont"><strong>不 当 奴 隶 ， 更 不 当 奴 才 ─ ─ 纪 念 耀 邦 逝 世 20 周 年 </strong><br />
<br />
李 锐, 苹 果 日 报, 2009-4-15<br />
<br />
上 个 世 纪 70 年 代 末 ， 中 国 刚 刚 走 出 「 文 革 」 动 乱 ， 却 依 旧 被 极 左 阴 霾 和 桎 梏 所 笼 罩 禁 锢 。 冲 破 阴 霾 和 打 破 桎 梏 的 ， 是 胡 耀 邦 发 动 丶 领 导 和 全 力 推 动 的 振 聋 发 聩 的 真 理 标 准 问 题 大 讨 论 和 w 假 错 案 的 大 平 反 。 他 以 非 凡 的 勇 气 ， 破 冰 般 打 开 思 想 解 放 的 局 面 ， 掀 开 了 改 革 开 放 大 潮 的 序 幕 。 今 年 4 月 ， 是 耀 邦 逝 世 20 周 年 ， 我 谨 以 此 文 向 他 表 示 深 切 的 哀 悼 和 缅 怀 。 为 什 麽 用 这 样 一 个 题 目 呢 ？ <strong>还 是 借 用 鲁 迅 的 说 法 ： 中 国 自 古 以 来 ， 专 制 王 朝 ， 老 百 姓 当 惯 奴 隶 ， 少 数 官 员 效 忠 当 奴 才 。 过 去 共 产 党 是 必 须 当 驯 服 工 具 ， 群 众 学 雷 锋 当 螺 丝 钉 ， 少 数 奴 才 们 助 纣 为 虐 。</strong><br />
<br />
<br />
<strong>不 正 常 的 党 内 生 活</strong><br />
<br />
要 谈 胡 耀 邦 ， 不 得 不 先 谈 中 国 共 产 党 。 自 建 党 以 来 ， 任 何 党 员 都 是 党 的 驯 服 工 具 ， 这 个 观 念 深 入 人 心 ， 也 作 为 组 织 原 则 贯 彻 下 去 。 作 为 一 个 实 质 上 的 农 民 党 ， 毛 泽 东 是 中 国 共 产 党 最 重 要 的 缔 造 者 。 毛 泽 东 这 个 人 很 复 杂 ， 毛 的 周 围 有 一 帮 人 。 <br />
<br />
毛 泽 东 最 喜 欢 的 人 是 高 岗 丶 林 彪 和 邓 小 平 ， 曾 打 算 让 高 岗 当 接 班 人 。 高 岗 有 他 的 本 事 ， 属 於 「 绿 林 豪 杰 」 ， 毛 很 欣 赏 。 我 当 过 高 岗 的 政 治 秘 书 ， 他 喜 欢 下 围 棋 ， 晚 上 同 我 下 棋 是 从 不 接 电 话 的 。 离 休 後 我 负 责 编 中 共 组 织 史 资 料 ， 从 打 AB 团 起 ， 10 年 内 战 肃 反 ， 自 己 杀 自 己 人 ， 杀 了 10 万 。 延 安 时 期 抢 救 运 动 打 了 1 万 5 千 个 特 务 ， 但 事 实 上 一 个 打 入 党 内 的 特 务 都 没 有 。 我 问 过 黄 克 诚 ， 在 江 西 时 他 就 感 到 ， 毛 有 两 个 毛 病 ： 一 是 脾 气 太 坏 ， 二 是 任 人 唯 亲 。 井 冈 山 下 来 在 福 建 选 前 委 ， 多 数 人 投 陈 毅 的 票 ， 毛 泽 东 就 甩 手 不 干 了 。 後 来 陈 毅 去 上 海 找 周 恩 来 ， 周 让 陈 赶 紧 把 毛 请 回 来 。 如 萧 克 没 投 毛 的 票 ， 毛 对 萧 一 直 记 在 心 上 。 <br />
<br />
没 有 林 彪 的 吹 捧 与 参 与 ， 「 文 化 大 革 命 」 可 能 搞 不 起 来 ； 「 林 彪 事 件 」 後 毛 泽 东 就 垮 了 。 後 来 接 班 的 邓 小 平 ， 其 实 也 是 半 个 毛 泽 东 。 1989 年 「 六 四 」 风 波 ， 出 动 军 队 镇 压 学 生 运 动 ， 这 种 连 毛 泽 东 活 着 都 不 可 能 干 出 来 的 事 ， 邓 小 平 却 干 了 。 「 六 四 」 期 间 ， 他 听 信 李 鹏 和 李 锡 铭 谎 报 「 学 生 动 乱 」 的 谗 言 ， 要 实 行 军 事 戒 严 ， 萧 克 丶 张 爱 萍 等 7 位 上 将 联 名 上 书 反 对 武 力 镇 压 ， 他 毫 不 理 睬 。 <br />
<br />
总 之 ， 80 年 代 的 局 势 对 耀 邦 来 说 ， 很 难 办 ， 上 面 两 个 老 人 压 着 。 邓 小 平 赞 成 「 权 威 主 义 」 ， 他 认 为 中 国 总 得 有 「 一 个 人 说 了 算 」 。 耀 邦 告 诉 过 我 ， 邓 小 平 与 陈 云 这 两 位 政 治 老 人 的 关 系 ， 按 邓 小 平 的 话 说 ， 是 「 谈 不 拢 」 ， 胡 耀 邦 在 位 时 ， 就 开 不 成 常 委 会 ； 「 只 能 有 一 个 婆 婆 」 ， 这 是 邓 小 平 让 薄 一 波 向 陈 云 传 达 的 。 改 革 开 放 ， 邓 小 平 在 经 济 上 比 较 坚 定 ， 如 成 立 「 特 区 」 ， 这 是 一 个 创 举 。 而 陈 云 仍 坚 持 「 鸟 笼 政 策 」 ， 这 是 原 则 性 分 歧 。 在 意 识 形 态 上 ， 邓 小 平 「 不 问 姓 资 姓 社 」 ， 「 不 管 黑 猫 白 猫 」 ， 「 摸 着 石 头 过 河 」 ， 不 为 既 往 的 正 统 理 论 所 限 制 ， 敢 於 突 破 ， 这 是 有 极 大 功 劳 的 。 而 陈 云 仍 跳 不 出 旧 框 框 ， 1989 年 ， 中 顾 委 发 了 个 「 一 号 文 件 」 ， 刊 载 了 陈 云 同 赵 紫 阳 丶 胡 启 立 丶 李 瑞 环 1987 年 以 後 的 谈 话 ， 他 说 ： 「 列 宁 帝 国 主 义 论 没 有 过 时 」 。 对 「 六 四 」 风 波 ， 陈 云 则 有 自 己 的 看 法 。 当 时 在 中 顾 委 ， 我 们 四 个 人 （ 杜 润 生 丶 李 昌 丶 于 光 远 和 我 ） 反 对 镇 压 ， 几 个 月 批 斗 ， 将 要 开 除 党 籍 ， 是 陈 云 挽 救 了 我 们 ； 他 说 ， 这 种 事 不 能 再 干 了 ， 否 则 ， 以 後 还 要 平 反 。 <br />
<br />
陈 云 对 胡 耀 邦 有 看 法 ， 认 为 他 在 人 事 上 用 「 青 红 帮 」 （ 青 年 团 丶 红 卫 兵 ） 。 1982 年 初 我 已 经 65 岁 ， 决 定 在 电 力 部 退 下 来 。 这 时 陈 云 要 我 到 中 央 组 织 部 组 建 青 年 干 部 局 ， 意 见 极 其 坚 决 ， 促 我 上 任 ， 还 误 会 我 是 「 能 上 不 能 下 」 丶 不 愿 当 局 长 。 於 是 我 只 好 服 从 ， 於 该 年 3 月 调 入 中 央 组 织 部 。 上 任 後 我 只 见 他 一 次 ， 见 了 面 就 说 要 「 顶 住 」 ， 言 下 之 意 就 是 顶 住 胡 耀 邦 。 1987 年 耀 邦 在 「 生 活 会 」 上 被 以 前 的 战 友 王 鹤 寿 揭 发 ， 非 常 伤 心 ， 他 曾 对 王 鹤 寿 讲 过 一 些 心 里 话 。 王 鹤 寿 的 侄 女 後 来 向 我 透 露 ， 揭 发 耀 邦 是 陈 云 的 命 令 。<br />
<br />
除 了 两 个 老 人 ， 当 时 还 有 两 个 「 左 王 」 看 不 起 他 ， 在 两 老 之 间 讲 闲 话 ， 一 个 是 胡 乔 木 ， 一 个 是 邓 力 群 。 <br />
<br />
胡 乔 木 这 个 人 ， 「 一 日 无 君 则 惶 惶 然 」 。 改 革 开 放 以 後 ， 主 要 在 意 识 形 态 上 ， 胡 乔 木 仍 坚 持 毛 泽 东 「 政 治 挂 帅 」 的 路 线 ， 实 质 上 是 「 以 阶 级 斗 争 为 纲 」 ， 不 过 不 明 说 罢 了 。 他 是 个 两 面 派 ， 真 正 的 两 面 派 ， 今 天 这 样 明 天 又 翻 过 来 。 我 跟 胡 乔 木 的 渊 源 很 深 ， 对 他 比 较 了 解 。 解 放 初 我 在 湖 南 工 作 的 时 候 ， 曾 收 集 了 毛 主 席 20 几 篇 旧 作 ， 印 了 50 本 ， 给 中 宣 部 1 本 ， 胡 乔 木 要 求 全 部 上 交 ， 责 备 我 「 此 种 事 ， 有 害 无 益 。 」 我 年 少 气 盛 ， 写 了 一 本 《 毛 泽 东 的 初 期 革 命 活 动 》 。 1952 年 我 调 到 北 京 的 时 候 ， 他 给 我 打 电 话 ， 居 然 又 要 我 到 中 宣 部 管 党 史 。 没 有 他 支 持 ， 《 庐 山 会 议 实 录 》 也 出 不 来 啊 ！ 张 闻 天 纪 念 文 集 出 版 前 ， 夫 人 刘 英 写 信 要 我 写 篇 文 章 ， 後 来 胡 乔 木 把 书 的 主 编 叫 去 加 以 训 斥 ： 「 你 干 嘛 让 李 锐 写 文 章 ？ 」 主 编 说 是 刘 大 姐 让 写 的 ， 他 才 不 吱 声 了 。 像 胡 乔 木 那 样 的 人 ， 不 当 奴 才 不 行 啊 ！ 他 愿 意 当 奴 才 ， 因 为 有 好 处 。 中 国 历 史 上 有 很 多 这 种 人 物 。 <br />
<br />
1986 年 ， 邓 力 群 把 陆 铿 同 胡 耀 邦 的 访 谈 交 给 邓 小 平 ， 引 起 邓 的 震 怒 ， 这 也 成 为 1987 年 1 月 「 生 活 会 」 後 胡 耀 邦 下 台 的 一 根 导 火 索 。 1987 年 春 ， 陈 云 让 王 震 等 人 活 动 ， 把 邓 力 群 抬 出 来 当 总 书 记 。 我 在 1987 年 7 月 11 日 给 邓 小 平 丶 赵 紫 阳 写 了 一 封 信 表 示 反 对 ， 除 了 揭 发 他 抵 制 改 革 开 放 外 ， 还 述 及 他 在 延 安 抢 救 运 动 时 犯 的 一 件 严 重 政 治 错 误 。 邓 小 平 14 日 批 示 ， 撤 销 邓 力 群 的 职 务 ， 陈 云 丶 李 先 念 丶 薄 一 波 三 人 也 画 圈 了 ， 制 止 了 这 个 危 机 。 <br />
<br />
除 了 一 些 小 事 ， 胡 耀 邦 和 赵 紫 阳 合 作 比 较 融 洽 ， 对 经 济 体 制 和 政 治 体 制 全 面 改 革 的 看 法 也 基 本 一 致 。 但 邓 小 平 丶 陈 云 两 位 老 人 对 全 面 改 革 却 有 不 一 致 的 看 法 。 那 两 个 「 左 王 」 趁 机 在 邓 小 平 丶 陈 云 两 位 老 人 中 穿 梭 ， 大 进 谗 言 。 这 两 个 「 左 王 」 对 改 革 开 放 起 了 很 坏 的 作 用 ， 尤 其 是 在 思 想 文 化 领 域 ， 先 搞 「 清 除 精 神 污 染 」 ， 後 又 批 「 资 产 阶 级 自 由 化 」 。 清 污 只 搞 了 28 天 ， 幸 好 被 胡 丶 赵 联 手 制 止 了 。 <br />
<br />
1987 年 的 「 生 活 会 」 上 ， 耀 邦 被 迫 作 了 检 查 ， 听 说 离 开 会 场 後 即 失 声 痛 哭 。 一 个 人 如 果 不 是 因 受 大 委 屈 而 伤 心 透 了 ， 是 不 会 这 样 大 哭 的 ， 尤 其 是 在 这 种 场 合 。 他 在 担 任 总 书 记 时 就 说 过 ： 我 尊 重 老 人 ， 又 独 立 思 考 。 他 尽 力 在 两 位 老 人 之 间 沟 通 协 调 ， 遇 大 事 必 请 示 ， 同 时 还 要 面 对 两 个 「 左 王 」 的 明 枪 暗 箭 与 各 种 阻 挠 ， 最 後 遭 到 两 方 面 夹 击 ， 个 人 事 小 ， 全 局 堪 忧 ， 他 怎 能 不 放 声 大 哭 ！<br />
<br />
<strong>胡 耀 邦 是 一 个 什 麽 样 的 人</strong><br />
<br />
胡 耀 邦 是 红 小 鬼 ， 十 四 五 岁 就 参 加 革 命 ， 文 化 程 度 不 高 。 他 碰 到 第 一 件 事 是 打 AB 团 ， 几 乎 丧 命 ， 此 事 终 生 难 忘 。 此 後 历 次 政 治 运 动 直 到 「 文 化 大 革 命 」 ， 他 都 尽 可 能 予 以 抵 制 或 在 力 所 能 及 范 围 内 做 有 益 之 事 。 耀 邦 历 来 对 「 政 治 挂 帅 」 有 自 己 的 看 法 ， 不 喜 欢 搞 条 条 框 框 ， 坚 守 言 行 一 致 。 「 大 跃 进 」 失 败 後 ， 1960 年 就 主 张 恢 复 长 途 贩 运 ； 他 曾 公 开 讲 话 ： 「 衣 食 足 而 知 廉 耻 ， 学 习 毛 主 席 着 作 不 能 饱 肚 子 」 ， 不 怕 被 扣 「 修 正 主 义 」 帽 子 。 <br />
<br />
胡 耀 邦 能 在 共 产 党 内 出 现 ， 非 常 了 不 起 ， 战 争 年 代 出 来 的 人 ， 服 从 惯 了 ， 更 不 容 易 了 。 他 独 立 思 考 ， 他 不 当 奴 隶 ， 更 不 愿 意 当 奴 才 。 延 安 抢 救 运 动 时 ， 到 处 大 抓 特 务 ， 他 当 时 担 任 总 政 组 织 部 长 ， 自 己 做 了 个 试 验 ， 找 几 个 绝 对 没 有 问 题 的 年 青 人 ， 一 逼 供 信 ， 就 都 承 认 是 特 务 了 。 於 是 他 立 即 找 毛 主 席 说 ， 不 能 再 干 这 种 事 情 。 这 是 他 亲 口 对 我 说 的 。 <br />
<br />
5 年 前 ， 耀 邦 去 世 15 周 年 时 ， 几 位 团 中 央 的 老 同 志 编 写 《 胡 耀 邦 传 》 ， 我 为 这 本 书 写 过 一 个 序 言 。 这 个 序 言 里 提 到 ： <br />
<br />
1950 年 到 1952 年 ， 担 任 川 北 党 政 领 导 时 ， 耀 邦 亲 自 起 草 「 人 民 代 表 公 约 」 ； 土 改 中 实 行 保 护 富 农 和 对 地 主 的 温 和 政 策 ； 不 许 农 民 乱 罚 乱 斗 ， 进 城 抓 人 。 当 年 邓 小 平 这 样 评 价 他 ： 「 有 主 见 ， 不 盲 从 。 」 <br />
<br />
1952 年 到 1966 年 ， 他 担 任 团 中 央 第 一 书 记 ， 提 倡 八 个 大 字 ： 「 朝 气 蓬 勃 ， 实 事 求 是 。 」 他 说 ： 「 实 事 求 是 就 是 做 老 实 人 ， 说 老 实 话 ， 办 老 实 事 。 要 说 一 是 一 ， 说 二 是 二 ， 不 弄 虚 作 假 。 要 有 实 干 的 精 神 ， 言 行 一 致 ， 表 里 如 一 」 ， 他 认 为 执 政 党 尤 其 要 这 样 要 求 。 战 争 年 代 ， 形 势 逼 得 你 非 实 事 求 是 不 可 ； 执 政 以 後 ， 即 容 易 自 以 为 是 ， 走 向 反 面 ； 坚 持 实 事 求 是 ， 必 须 深 入 实 际 ， 尤 其 需 要 民 主 制 度 和 党 的 纪 律 的 保 证 。 <br />
<br />
1954 年 10 月 ， 《 中 国 青 年 》 编 辑 问 他 ， 「 怎 样 理 解 社 会 主 义 人 道 主 义 ？ 」 耀 邦 回 答 ： 「 无 产 阶 级 不 是 只 讲 革 命 ， 只 讲 斗 争 ， 它 也 讲 爱 心 ， 讲 人 情 味 ， 讲 对 同 志 丶 对 广 大 人 民 群 众 的 尊 重 丶 关 心 和 爱 护 。 只 有 这 样 ， 我 们 的 社 会 主 义 才 是 可 爱 的 ， 人 们 才 能 从 中 感 受 到 更 多 的 欢 乐 和 温 暖 。 」 <br />
<br />
1957 年 反 右 派 时 ， 他 不 在 国 内 ； 回 国 後 ， 对 团 中 央 打 的 右 派 进 行 安 慰 。 他 同 我 谈 过 ， 生 平 憾 事 ， 对 项 南 和 苏 进 没 有 保 护 好 ； 两 次 自 责 ， 批 斗 彭 德 怀 时 没 讲 公 道 话 ， 开 除 刘 少 奇 党 籍 也 举 了 手 。 <br />
<br />
1962 年 下 放 湖 南 帮 助 工 作 两 年 时 ， 他 亲 自 到 浏 阳 丶 醴 陵 丶 平 江 的 生 产 大 队 蹲 点 。 四 清 运 动 开 始 ， 对 政 策 界 限 和 方 法 步 骤 都 作 了 规 定 ： 凡 属 集 体 瞒 产 私 分 ， 不 做 处 理 ； 手 脚 不 乾 净 的 ， 公 物 归 还 ， 不 搞 坦 白 检 举 ； 群 众 向 干 部 提 意 见 ， 只 「 背 靠 背 」 ； 同 时 号 召 「 一 手 抓 生 产 ， 一 手 抓 运 动 。 」 湘 潭 地 区 因 此 没 有 发 生 乱 斗 和 影 响 生 产 ， 大 家 都 满 意 。<br />
<br />
1964 年 12 月 到 1965 年 6 月 ， 在 陕 西 第 一 书 记 任 上 ， 正 是 以 阶 级 斗 争 为 纲 丶 四 清 运 动 越 来 越 左 时 。 他 以 大 无 畏 的 精 神 ， 开 展 了 「 解 放 思 想 丶 解 放 人 丶 放 宽 政 策 丶 搞 活 经 济 」 为 主 题 的 超 前 民 主 改 革 ， 纠 正 社 教 运 动 中 侵 犯 人 权 的 错 误 作 法 。 发 出 这 样 的 号 召 ： 「 社 教 运 动 是 教 育 人 ， 不 是 整 人 」 ； 「 要 维 护 人 权 ， 尊 重 风 俗 人 情 」 ； 「 民 主 要 过 硬 」 ； 「 领 导 人 要 听 反 对 的 话 」 。 他 强 调 「 生 产 好 不 好 ， 是 检 验 工 作 好 不 好 的 最 主 要 标 志 。 」 同 时 ， 恢 复 集 市 贸 易 ， 允 许 短 途 运 输 ， 发 展 乡 镇 企 业 ， 提 倡 植 树 造 林 （ 谁 种 归 谁 ） 。 当 年 在 西 北 局 的 领 导 下 ， 陕 西 「 左 祸 」 特 别 严 重 ， 耀 邦 本 人 被 叶 剑 英 保 护 先 行 回 到 北 京 ， 他 的 副 手 等 则 受 到 十 年 迫 害 。 <br />
<br />
耀 邦 在 「 文 革 」 中 始 终 是 清 醒 的 。 初 期 同 团 中 央 几 位 书 记 同 舟 共 济 ， 每 天 有 几 千 上 万 人 来 揪 斗 他 们 。 有 人 揭 发 他 反 对 毛 主 席 ， 说 过 「 太 阳 也 有 黑 点 」 ； 反 对 林 副 主 席 「 突 出 政 治 」 ， 说 ： 「 游 泳 时 要 突 出 鼻 子 ， 不 然 就 要 呛 水 」 ； 他 说 过 「 康 生 一 贯 左 」 。 他 只 承 认 学 习 不 够 ， 工 作 有 错 ， 执 行 了 修 正 主 义 路 线 。 有 次 在 长 辛 店 ， 被 打 得 全 身 皮 肉 红 肿 。 他 认 为 毛 泽 东 骄 傲 了 ； 「 不 让 权 ， 不 做 自 我 批 评 的 。 」 「 骄 傲 害 死 人 呀 ！ 」 他 曾 叹 息 「 搞 了 八 年 还 看 不 到 头 」 ， 「 多 行 不 义 必 自 毙 」 。 <br />
<br />
文 革 後 期 ， 耀 邦 负 责 科 学 院 工 作 ， 主 持 起 草 了 《 汇 报 提 纲 》 。 根 据 马 克 思 的 着 作 ， 最 早 提 出 「 科 学 技 术 是 生 产 力 」 ， 後 而 否 定 了 「 知 识 私 有 」 丶 「 白 专 道 路 」 等 错 误 提 法 ， 并 作 了 「 实 现 四 个 现 代 化 是 新 长 征 」 的 报 告 。 後 来 在 「 批 邓 反 击 右 倾 翻 案 风 」 中 ， 这 《 汇 报 提 纲 》 成 了 集 中 批 判 的 「 三 株 大 毒 草 」 之 一 ， 他 又 受 到 残 酷 斗 争 。 <br />
<br />
由 於 上 述 这 些 事 例 ， 我 们 知 道 耀 邦 一 生 历 经 革 命 磨 练 ， 特 别 注 重 理 论 实 践 的 结 合 ， 尤 其 看 重 实 践 效 果 ， 因 而 能 抓 住 过 去 党 和 毛 泽 东 屡 犯 错 误 最 终 走 上 文 革 绝 路 的 症 结 ， 那 就 是 毛 接 受 了 斯 大 林 的 一 套 做 法 ， 从 政 治 丶 经 济 到 文 化 丶 思 想 的 绝 对 统 治 ， 名 为 无 产 阶 级 专 政 实 为 一 党 专 政 ， 一 党 专 政 又 变 成 了 领 袖 专 政 。<br />
<br />
<strong>耀 邦 对 中 国 问 题 的 反 思</strong><br />
<br />
从 投 身 革 命 到 最 後 辞 世 ， 胡 耀 邦 在 党 内 生 活 了 60 年 。 对 这 段 漫 长 而 沉 重 的 历 史 ， 他 有 深 刻 的 反 思 ； 对 当 代 中 国 的 痼 疾 ， 他 有 透 辟 的 洞 察 。 <strong>反 思 和 洞 察 的 结 果 ， 使 他 形 成 了 许 多 反 映 和 顺 应 人 类 文 明 主 流 的 思 想 丶 观 点 和 理 念 。 耀 邦 认 为 ， 党 不 能 再 受 「 左 」 的 危 害 ； 绝 对 不 能 迷 信 任 何 人 ， 而 应 该 独 立 思 考 ； 党 和 国 家 生 活 应 该 正 常 化 丶 民 主 化 丶 法 治 化 ， 必 须 杜 绝 家 长 制 丶 一 言 堂 丶 一 个 人 说 了 算 ； 要 爱 护 知 识 分 子 丶 尽 力 发 扬 知 识 分 子 的 才 能 ； 应 当 重 视 自 由 丶 民 主 丶 人 道 和 法 治 原 则 ， 吸 取 人 类 文 明 共 同 的 优 秀 成 果 。 改 革 开 放 以 来 ， 他 提 出 和 实 施 的 许 多 政 策 主 张 ， 尤 其 坚 持 经 济 和 政 治 体 制 改 革 必 须 同 步 ， 都 体 现 出 一 种 超 越 原 有 意 识 形 态 的 人 类 普 适 价 值 。</strong> 即 便 发 生 1987 年 的 「 辞 职 事 件 」 後 ， 他 的 思 考 仍 然 没 有 停 止 ， 反 而 更 加 深 刻 。 一 个 共 产 党 的 最 高 领 导 人 ， 从 人 类 文 明 的 视 野 ， 来 思 考 国 家 丶 民 族 丶 政 党 的 前 行 方 向 ， 过 去 在 我 们 党 内 ， 确 实 是 鲜 见 的 。 <br />
<br />
1979 年 3 月 25 日 ， 西 单 民 主 墙 出 现 《 要 民 主 还 是 要 新 的 独 裁 》 丶 《 中 国 人 权 宣 言 》 等 大 字 报 ， 随 後 作 者 魏 京 生 等 被 捕 ， 耀 邦 就 极 不 以 为 然 。 他 给 一 位 青 年 写 了 一 封 公 开 信 ， 在 《 人 民 日 报 》 4 月 10 日 头 版 发 表 。 信 中 说 ： 「 教 育 青 年 的 方 法 ， 不 是 压 ， 不 是 抓 ， 应 该 是 『 引 导 』 两 个 字 。 『 引 导 』 比 『 教 育 』 更 精 确 ， 意 义 更 大 。 <br />
<br />
这 是 我 们 几 十 年 工 作 的 经 验 总 结 。 压 制 的 方 法 ， 一 个 巴 掌 打 下 去 ， 是 封 建 家 长 的 办 法 。 」 随 後 6 月 间 ， 他 又 在 五 届 人 大 二 次 会 议 的 发 言 中 ， 更 加 严 正 地 对 取 缔 民 主 墙 和 逮 捕 魏 京 生 等 人 发 表 了 意 见 ： 「 我 始 终 支 持 任 何 人 在 社 会 主 义 制 度 下 行 使 自 己 的 民 主 权 利 。 希 望 大 家 都 在 宪 法 的 保 护 下 享 有 最 大 的 自 由 。 尽 管 在 中 央 工 作 会 议 上 ， 以 及 这 次 人 大 会 议 上 ， 不 少 同 志 点 名 也 好 不 点 名 也 好 ， 批 评 我 背 着 中 央 ， 支 持 违 反 『 四 项 基 本 原 则 』 的 所 谓 民 主 化 运 动 ， 助 长 无 政 府 主 义 ， 但 我 仍 要 保 持 我 自 己 的 看 法 。 」 「 我 奉 劝 同 志 们 不 要 抓 人 来 斗 ， 更 不 要 抓 人 来 关 。 敢 於 大 胆 提 出 这 些 问 题 的 人 ， 恐 怕 也 不 在 乎 坐 牢 。 魏 京 生 抓 了 三 个 多 月 ， 至 今 没 有 作 过 检 讨 。 听 说 他 现 在 还 在 绝 食 。 他 一 死 就 会 在 群 众 中 成 为 烈 士 ， 是 人 们 心 中 的 烈 士 。 」 （ 请 问 ， 我 们 现 在 还 能 在 中 央 的 会 议 上 听 到 哪 个 领 导 人 发 出 这 样 的 声 音 吗 ？ ） 耀 邦 当 时 还 主 张 成 立 一 个 民 主 公 园 。 後 来 ， 北 京 市 虽 然 将 月 坛 公 园 定 为 民 主 公 园 ， 由 於 政 治 形 势 影 响 ， 无 疾 而 终 。 当 年 党 内 老 人 支 持 耀 邦 的 只 有 叶 剑 英 。 叶 说 过 ： 「 党 的 十 一 届 三 中 全 会 是 党 内 民 主 的 典 范 ， 西 单 民 主 墙 是 人 民 民 主 的 典 范 。 」<br />
<br />
除 了 独 立 思 考 外 ， 耀 邦 还 很 喜 欢 读 书 ， 重 视 读 书 ， 在 延 安 毛 泽 东 就 曾 夸 奖 他 「 思 想 敏 锐 丶 口 若 悬 河 」 。 虽 然 他 只 读 过 一 年 初 中 ， 但 他 善 於 博 览 群 书 ， 读 过 《 马 恩 全 集 》 ， 也 读 过 《 圣 经 》 。 他 做 报 告 不 念 稿 子 ， 会 写 文 章 ， 会 作 旧 诗 ， 字 也 写 得 好 ， 因 此 被 陆 定 一 称 为 大 知 识 分 子 。 他 常 讲 要 爱 护 知 识 分 子 ， 并 尽 力 保 护 他 们 当 中 挨 过 整 的 人 。 「 文 革 」 後 坚 决 反 对 过 去 整 人 的 那 一 套 ， 主 张 真 正 实 行 「 双 百 」 方 针 ； 不 是 停 在 口 头 上 ， 而 是 认 真 贯 彻 於 一 切 方 面 ， 要 见 诸 行 动 。 1987 年 下 台 以 後 的 两 年 ， 耀 邦 更 是 积 极 地 读 书 ， 要 把 人 类 的 这 些 事 情 搞 清 楚 。 而 毛 泽 东 就 不 同 ， 他 不 喜 欢 知 识 分 子 ， 称 为 「 皮 上 之 毛 」 ， 甚 至 说 「 知 识 越 多 越 蠢 ， 越 多 越 反 动 。 」 要 知 道 科 学 知 识 及 其 载 体 ， 乃 人 类 社 会 进 步 的 根 本 动 力 。 耀 邦 是 理 解 这 个 道 理 的 。 <br />
<br />
无 论 处 於 何 种 位 置 ， 耀 邦 总 是 平 易 近 人 ， 平 等 待 人 ； 他 喜 欢 随 意 交 谈 ， 讲 心 里 话 ， 能 虚 心 地 听 人 家 的 意 见 ； 他 是 一 个 讲 人 格 丶 讲 人 性 丶 讲 人 情 的 人 。 耀 邦 在 工 作 中 重 视 实 情 ， 常 往 下 跑 ， 曾 经 发 誓 要 走 遍 全 国 两 千 多 县 （ 大 概 完 成 了 一 半 多 ， 此 举 却 受 到 讥 评 ） 。 晚 年 後 虽 然 身 体 不 好 ， 他 还 去 西 藏 考 察 。 <br />
<br />
当 然 ， 胡 耀 邦 有 他 自 身 的 缺 点 ， 就 是 天 真 。 我 问 他 当 年 为 什 麽 要 做 检 讨 ， 他 说 ， 我 要 照 顾 这 个 家 啊 ！ 到 了 形 势 最 危 急 的 时 候 ， 他 不 认 真 反 抗 了 。 另 外 ， 他 身 边 没 有 得 力 助 手 ； 赵 紫 阳 跟 他 就 很 不 同 ， 赵 有 智 囊 团 ， 紧 急 关 头 ， 他 找 人 座 谈 求 教 。 耀 邦 同 我 谈 过 这 样 一 件 事 ： 胡 乔 木 写 信 给 他 一 个 建 议 ， 乔 木 是 完 全 错 了 ， 他 覆 信 说 明 ， 还 把 原 信 退 回 ， 并 没 有 留 在 手 中 当 作 把 柄 。 虽 然 他 遇 事 总 要 深 思 熟 虑 ， 但 有 时 过 於 热 心 ， 讲 话 多 了 难 免 有 失 。 他 不 弄 权 术 ， 从 不 设 防 ， 有 时 遭 人 暗 算 而 不 觉 。 他 是 一 个 非 常 宽 厚 的 人 ， 「 文 革 」 後 他 的 「 多 换 思 想 少 换 人 」 的 干 部 政 策 ， 引 起 过 别 人 的 意 见 。 <br />
<br />
「 文 革 」 以 後 ， 无 论 是 开 理 论 务 虚 会 ， 尤 其 彻 底 平 反 w 假 错 案 ， 除 了 胡 耀 邦 ， 党 内 很 少 有 人 能 如 此 大 干 。 之 後 ， 不 论 遇 到 何 种 干 扰 ， 他 总 是 坚 持 政 治 体 制 改 革 必 须 同 经 济 改 革 同 步 ； 他 认 同 人 类 社 会 进 步 的 普 世 规 律 ， 强 调 必 须 走 民 主 丶 科 学 丶 法 治 之 道 ； 他 力 促 舆 论 开 放 丶 言 论 自 由 ， 反 对 「 左 爷 」 们 的 种 种 阴 谋 干 预 。 胡 耀 邦 唯 一 的 缺 点 就 是 不 懂 得 保 护 自 己 ， 这 很 可 惜 ， 他 不 懂 得 自 己 对 这 个 国 家 的 重 要 性 ， 而 且 他 下 台 以 後 也 影 响 到 了 赵 紫 阳 。 对 老 人 的 心 计 ， 他 估 计 不 够 。<br />
<br />
<strong>推 进 改 革 ， 完 成 耀 邦 的 遗 愿</strong><br />
<br />
胡 耀 邦 去 世 ， 是 中 国 很 大 的 不 幸 ， 也 是 一 切 以 他 为 师 丶 为 友 丶 为 长 者 丶 为 楷 模 的 人 很 大 不 幸 。 但 中 国 出 了 个 胡 耀 邦 ， 共 产 党 里 出 了 个 胡 耀 邦 ， 这 又 是 中 国 的 大 幸 。 <br />
<br />
胡 耀 邦 否 定 「 文 革 」 最 彻 底 ， 在 「 真 理 标 准 」 的 讨 论 中 最 坚 定 。 他 认 为 党 不 能 再 受 「 左 」 的 危 害 ， 对 於 「 左 」 祸 肆 虐 的 记 忆 可 谓 刻 骨 铭 心 。 他 重 视 自 由 丶 民 主 丶 人 道 ， 熟 悉 西 方 的 发 展 历 史 ， 很 愿 意 接 受 外 来 的 新 思 想 。 他 对 毛 泽 东 的 认 识 是 全 面 的 ， 在 「 文 革 」 时 便 不 盲 从 ， 常 说 绝 对 不 能 迷 信 任 何 人 ， 要 独 立 思 考 。 他 力 主 党 内 生 活 正 常 化 丶 民 主 化 ， 应 健 康 发 展 ， 一 定 要 避 免 过 去 搞 家 长 制 丶 一 人 说 了 算 的 错 误 ， 并 主 持 了 「 党 内 政 治 生 活 若 干 准 则 」 的 起 草 。 <br />
<br />
1989 年 4 月 20 日 ， 赵 紫 阳 代 表 中 共 中 央 在 胡 耀 邦 追 悼 大 会 上 对 耀 邦 一 生 作 了 评 价 ， 悼 词 是 经 过 邓 小 平 和 中 央 负 责 同 志 讨 论 过 的 ， 还 是 比 较 公 正 的 。 但 由 於 1987 年 1 月 「 生 活 会 」 上 强 加 给 他 的 那 些 不 实 之 词 ， 以 及 迫 使 他 下 台 的 做 法 ， 曾 使 得 党 内 外 广 大 党 员 和 群 众 为 之 愤 愤 不 平 ， 以 致 後 来 引 发 了 「 六 四 」 政 治 风 波 。 对 胡 耀 邦 的 作 为 ， 要 有 一 个 全 面 公 正 符 合 实 际 的 总 结 ， 这 不 仅 是 他 个 人 的 问 题 ， 也 是 关 系 到 我 们 党 在 国 内 外 公 众 中 的 形 象 问 题 。 如 果 不 能 正 确 认 识 昨 天 ， 也 就 掌 握 不 好 今 天 和 明 天 。 <br />
<br />
今 天 ， 邓 小 平 丶 陈 云 两 位 老 人 已 不 在 了 ， 两 个 「 左 王 」 的 干 扰 也 不 存 在 ， 这 是 一 种 便 於 与 世 界 同 步 前 进 的 好 形 势 。 第 二 个 「 历 史 决 议 」 对 毛 泽 东 晚 年 错 误 做 过 总 结 ， 对 邓 小 平 也 应 当 照 此 办 理 。 这 样 ， 进 一 步 全 面 改 革 开 放 的 路 就 会 好 走 一 些 。 <br />
<br />
经 过 30 年 的 经 济 体 制 改 革 ， 我 国 的 经 济 总 量 已 跃 居 世 界 第 四 位 ， 但 代 价 太 大 ， 单 位 能 耗 和 对 资 源 丶 环 境 的 破 坏 也 居 世 界 前 列 。 中 国 现 在 的 经 济 总 量 大 概 占 全 世 界 的 15% ， 可 是 我 们 用 掉 的 煤 炭 大 概 占 世 界 三 分 之 一 。 更 可 怕 的 是 贪 污 丶 腐 败 愈 演 愈 烈 。 现 在 全 国 人 口 的 0.4% 掌 握 了 全 国 财 富 的 70% ， 贫 富 悬 殊 比 美 国 还 要 厉 害 （ 美 国 5% 的 人 口 掌 握 占 60% 的 财 富 ） 。 大 大 小 小 的 腐 败 分 子 上 下 其 手 ， 互 相 勾 结 ， 使 共 产 党 沦 向 了 一 个 利 益 集 团 丶 构 成 了 新 兴 的 权 贵 资 本 主 义 和 官 僚 资 本 主 义 。 <br />
<br />
近 几 年 来 ， 中 央 领 导 人 陆 续 提 出 了 「 和 谐 社 会 」 丶 「 以 人 为 本 」 丶 「 科 学 发 展 观 」 丶 「 物 质 丶 精 神 丶 政 治 三 大 文 明 」 等 治 国 方 针 ， 这 些 善 良 的 愿 望 明 显 优 於 30 年 前 的 「 四 个 坚 持 」 ， 但 要 真 正 落 实 ， 前 提 是 不 能 再 走 一 党 专 政 的 老 路 ， 必 须 按 照 促 进 人 类 社 会 进 步 的 普 世 价 值 规 律 ， 即 自 由 丶 民 主 丶 法 治 丶 宪 政 来 治 国 理 民 。 <br />
<br />
这 涉 及 到 政 治 体 制 改 革 这 个 老 大 难 的 问 题 ， 对 它 的 解 决 不 能 再 犹 豫 不 决 了 ！ 政 治 体 制 改 革 千 头 万 绪 ， <strong>我 建 议 首 先 从 开 放 言 论 自 由 丶 实 行 舆 论 监 督 ， 和 县 处 级 以 上 党 员 领 导 干 部 个 人 收 入 丶 家 庭 财 产 申 报 丶 公 示 入 手 ； 必 须 党 政 分 开 ， 政 企 分 开 ， 以 法 治 国 ， 实 施 宪 政 。</strong> 「 唯 一 忧 心 天 下 事 ， 何 时 宪 政 大 开 张 。 」 这 是 我 88 岁 自 寿 诗 中 的 尾 联 。 现 借 此 文 ， 再 次 呼 吁 ， 为 中 国 共 产 党 的 执 政 党 地 位 的 合 法 性 计 ， 为 国 家 丶 民 族 和 天 下 苍 生 计 ， 政 治 体 制 改 革 迫 在 眉 睫 ， 时 不 我 待 ！ <br />
<br />
胡 耀 邦 的 辞 职 和 辞 世 ， 是 上 个 世 纪 80 年 代 带 有 浓 烈 悲 剧 色 彩 的 一 幕 。 他 的 思 想 丶 理 念 丶 观 点 ， 迄 今 还 有 许 多 没 有 付 诸 实 践 ， 尤 其 政 治 体 制 改 革 的 滞 後 引 出 了 许 多 危 机 。 但 是 ， 无 论 如 何 ， 这 是 一 笔 宝 贵 的 精 神 遗 产 。 对 於 一 个 在 通 向 现 代 化 的 进 程 中 艰 难 跋 涉 的 民 族 和 国 家 来 说 ， 拥 有 这 笔 精 神 遗 产 也 实 为 大 幸 了 ！ <br />
