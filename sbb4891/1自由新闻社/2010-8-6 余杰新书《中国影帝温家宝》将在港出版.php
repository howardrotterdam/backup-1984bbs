													<h2>《中国影帝温家宝》8月16日香港上架</h2>
						
						
																											<div id="postmessage_773892" class="t_msgfont">文化人生 | 2010.08.06 <br />
《中国影帝温家宝》8月16日香港上架 <br />
 <br />
中国作家余杰的新书《中国影帝温家宝》计划于8月16日在香港与读者见面。余杰因为此书出版的消息曾于7月5日被警方传讯。他向德国之声表示，这本书能在短的时间内迅速被海内外关注，还多亏中国警方“帮忙”。 <br />
<br />
虽遭传讯仍坚持出版<br />
<br />
在中国警方７月初对余杰的传讯中，也提到了他即将出版的这本从名字上来说就会引起注意的书。按照余杰的讲述，当时中国警方说出许多威胁恐吓的话，试图让他放弃将此书出版的念头。并声称，温家宝不是一个普通的公民，温家宝是政府总理，所以批评他就涉及到危害国家安全，损害国家利益。<br />
<br />
尽管如此，余杰仍然打算将此书出版。谈到他如此坚持的原因，他对德国之声说： “我作为一个作家，对我来说言论自由是我生命中不能缺少的一部分。这样一本书所引用的材料，要么是中国当局在公开的媒体上发表过的，要么是国际权威媒体上发表过的，所以材料本身没有什么问题。这本书特殊的地方就在于我作为一个独立知识分子，我个人对这些材料的分析和评论。我觉得无论是中国现行的宪法，还是中国已经签署过的，联合国的人权宣言。都保障了我写这本书是公民的言论自由。” <br />
<br />
余杰表示，如果中国是一个有言论自由的国家，他希望此书能在中国大陆出版，而现在只能在香港出版就已经表明中国不是一个正常的，健康的社会。他说：“对我作为一个作者来说，我的潜在读者，我写作的对象是中国大陆的人。只能在香港出版已经让我觉得非常痛苦了。我这样最后的一点权利，我不会放弃。” <br />
<br />
余杰：中国警方是本书最大广告宣传方<br />
<br />
《中国影帝温家宝》一书首次发行量为5000册，据作者本人透露，一般他的书在香港出版的首印量都是在２０００册左右。这次首印量增长了一倍多，是许多媒体关注，以及读者向出版方表现出极大兴趣的结果。余杰说：“这是一个非常有趣的事情，如果因为不是7月5号北京警方来威胁我，来阻止这本书出版，使得这本书引起很多媒体的关注，很多读者的关心，它不会有这么大的出量，因为我原来在香港出版的书，第一版一般都是印2000本左右，所以我觉得北京警方反而成为这本书最大的广告宣传方。” <br />
<br />
乐观看待写作自由<br />
<br />
余杰表示7月5日过后，他没有再受到北京警方的直接骚扰。只是他于7月中旬赴香港参加书展时，被警方跟踪至机场。警方携海关人员对余杰的行李进行了“例行检查”。两篇他准备在香港中文大学作报告时用的讲演稿被海关当场没收。对此余杰持乐观态度：“但是我去香港仍然还是成行了，所以从目前的情况来看，还是有一定的往前走的可能性，就是这本书能够出版，我也能保持写作的自由。” <br />
<br />
余杰新书的出版方，香港新世纪出版社社长鲍朴向德国之声透露，此书出版前他本人曾多次警告余杰应该想清楚可能的后果。最后定夺出版也是余杰７月份到香港参加书展时做的决定。鲍朴向德国之声讲述了余杰作决定的过程：“今年书展的时候，２０几号，　他到香港来，我们见面以后，我说，如果你坚持要出这本书，我给你２４小时去好好想一想，再想一想，你明天告诉我。他说好。第二天我见到他以后呢，他说，哎，我们书要印几本啊？首印多少册啊？就说明他完全没有考虑到出版的后果。这是他本人的志向。” <br />
<br />
“皇帝新衣”中的小男孩<br />
<br />
鲍朴表示，他出版余杰的这本《中国影帝温家宝》是最大的考虑就是不想让作者出完书后进监狱，出版社唯一目的就是在不进监狱的条件下，还能够保证余杰一点点出版自由。因为他本人对这本书的特点的感受是：“我觉得这本书有一个特点，他写作，看问题的角度啊，太像皇帝的新衣里头那个小男孩了。所以我看了他的书又感慨，又想笑。看到他书以后我就觉得，如今还有人这样写书，一点恐惧感都没有。” <br />
<br />
鲍朴表示余杰不承认自己是持不同政见者，而只是一个独立作家。他唯一的要求就是写作没有恐惧。而新世纪出版社也只是一个出版社，而不是异议出版社。<br />
<br />
作者：任琛<br />
