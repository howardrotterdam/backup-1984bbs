																											<div id="postmessage_571286" class="t_msgfont"><a href="http://news.163.com/10/0419/14/64L0RSV40001124J.html" target="_blank">http://news.163.com/10/0419/14/64L0RSV40001124J.html</a><br />
<br />
<strong>胡锦涛题字黑板校方珍存 校长:将送博物馆珍藏</strong><br />
2010-04-19 14:35:54　来源: 法制晚报(北京)<br />
<br />
<i>核心提示：18日，胡锦涛在看望玉树州孤儿学校学生时，在七年级活动板房教室黑板上写下“新校园，会有的!新家园，会有的!”的话语，鼓励学生们好好学习。19日上午，该学校珍存胡锦涛题字黑板。校长尼玛仁增称，不便对外透露黑板存放的具体位置。</i><br />
<br />
法制晚报4月19日报道 今天上午，玉树州孤儿学校胡锦涛题字黑板被校方珍存。在换上新黑板并完成体检后，该校156名学生今晨全部复课。<br />
<br />
昨天下午，中共中央总书记、国家主席胡锦涛在看望玉树州孤儿学校学生时，在七年级活动板房教室黑板上写下“新校园，会有的!新家园，会有的!”的话语，鼓励学生们好好学习。<br />
<br />
今天上午，当记者再来到孤儿学校时，黑板上总书记的笔迹还在，一旁是1938年11月艾青的著名诗作《我爱这土地》的名句“我爱这土地爱得深沉”。<br />
<br />
校长尼玛仁增在题词黑板前告诉记者，黑板上总书记的题词不会擦的，并将完整珍存起来。未来将会送给博物馆，以作珍藏。<br />
<br />
上午10时全校师生上课前，约3米的可活动黑板被校方秘密收藏起来，校长尼玛仁增称，不便对外透露黑板存放的具体位置。<br />
<br />
而七年级全班同学在从教室墙角找出自己的书本后，便在老师的带领下开始晨读，朗诵起光未然创作于1939年抗日战争时期的语文课文《黄河颂》。而复课的其他班级，也均以朗诵语文为晨读内容。<br />
<br />
据了解，这是为了纪念该校唯一一名在地震中遇难的语文老师。<br />
<br />
新闻回放<br />
<br />
“新校园，会有的!新家园，会有的!”<br />
<br />
昨天下午，胡锦涛来到震中校舍受损严重的玉树州孤儿学校视察。因社会爱心组织紧急援建了活动板房，孩子们前天已正式复课。<br />
<br />
<br />
九年级学生正在板房教室上语文课，胡锦涛走进教室，同学生们交谈起来，询问他们的学习和生活情况。胡锦涛对大家说，听说你们在地震中不仅奋力自救，还救助他人，表现得都很勇敢。我还要衷心感谢精心照顾你们的老师和叔叔、阿姨们。<br />
<br />
胡锦涛对同学们说，地震毁掉了同学们的校舍，今天你们只能在临时校舍上课，但党和政府一定会为你们建设更好的校园。希望同学们努力学习，长大以后成为对国家、对人民有用的人才。<br />
<br />
说到这里，胡锦涛拿起粉笔，在黑板上一笔一划用力写下：“新校园，会有的!新家园，会有的!”写好后，他又领着孩子们一道大声念了起来。<br />
