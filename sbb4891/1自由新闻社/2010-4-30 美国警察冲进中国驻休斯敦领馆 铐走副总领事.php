<br />
　　环球网消息，据美国世界新闻网29日报道，当地时间24日晚间，中国驻休斯顿副总领事郁伯仁在总领馆附近遭到休斯顿警察围捕，期间美国警察手法粗暴，郁伯仁受伤被送往医院。此事已经掀起一场外交风波，中国政府已就此事向美国发出抗议。美国国务院表态称正在调查，并非常严肃地看待此事。<br />
<br />
　　据哥伦比亚广播公司29日披露，休斯顿警方以郁伯仁开的车子没挂牌照及未按要求停车为由，派出多辆警车进行追踪。当郁伯仁驾驶的车辆开进总领馆车库内时，多名美国警察冲进中国总领馆内，给郁伯仁铐上手铐。期间，郁伯仁副总领事的手和脖子受伤，被救护车送往医院治疗。郁伯仁副总领事在医院敷药后出院，目前已经恢复上班。<br />
<br />
　　休斯顿警察局目前还没有对事件发表任何评论，但哥伦比亚广播公司报道中引用的消息来源称，警方当时并不知道郁伯仁进入的是中国领事馆。不过在这一新闻的跟帖中，一些美国网民质疑警察不知道他们冲进去的是中国领事馆的说法，称警察可能是因为别的原因在追逐郁伯仁，而汽车牌照问题只是借口。有人表示，警察显然是像对待美国普通民众那样对待外交官。还有人指出，外交车牌被窃，在休斯顿是家常便饭。<br />
<br />
　　中国驻休斯敦总领馆王培军领事在接受环球网记者采访时表示，有关案件正在处理之中，中方要求美国严守《维也纳领事关系公约》和《中美领事条约》，确保中国外交领事人员及馆舍不受任何侵犯，中方将坚决维护中国外交领事人员的合法权益。<br />
<br />
　　美国国务院官员29日就此事作出表态，称美国务院正密切调查此事件及相关的执法权限。美方将与中国政府讨论调查结果。这位官员指出，“美国国务院非常严肃看待此事”，并已和中国大使馆就事件原委接触。美国国务院认为至关紧要的，就是(保护)所有在美国的外籍外交及领务人员的安全。</div>

							
							
							
							
