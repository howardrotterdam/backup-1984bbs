													<h2>广电总局：反对大量引进境外动画片</h2>
						
						
																											<div id="postmessage_733792" class="t_msgfont">国家广电总局官员：反对大量引进境外动画片<br />
2010年07月10日10:03中国新闻网廖勇斌我要评论(425) 字号：T|T<br />
中新网苏州7月10日电(廖勇斌)国家广电总局副总编辑、宣传管理司司长金德龙7月9日在2010苏州影视动漫创意投资大会上透露，短短的六年时间，国产动画的产量突飞猛进40倍。中国在积极推动国际合作，但反对大量引进境外动画片。<br />
<br />
针对中国是否会在政策上放宽对国外动漫作品的引进时，金德龙表示，中国引进的境外动画片，必须具有国际水准，必须具有引进国的优秀传统和文化，使我国的动画产业能够取长补短，借鉴其制作理念和优秀的创作观念，促进中国动画产业的发展，我们不能单纯地为引进而引进，引进的目的是为了促进国产动画产业的发展。<br />
<br />
他表示，中国在积极推动国际合作，但反对大量引进境外动画片，因为大量引进国外动画会使中国的动画产业受到重创，使中国动画产业难以崛起，使国内的少年儿童难以接受中国优秀传统文化的精华。<br />
<br />
谈到中国近几年动漫产业所取得的成绩时，金德龙表示，从2004年开始，中国出台了有关的一系列政策，从体制、市场、管理三个方面，推动中国动画产业的发展；其次是严格控制境外动画片对中国市场的倾销。实践证明，这些政策取得了明显的效果。<br />
<br />
他表示，1993年到2003年期间，中国的原创动画产量总共只有4.6万分钟，平均年产量不到4200分钟。2004年，中国广电总局出台了有关政策之后，当年的产量就达到了2.18万分钟，相当于前11年总量的一半。2005年，国产动画产量达到4.27万分钟，2006年，国产动画产量达到8.23万分钟，2007年，国产动画产量达到10.19万分钟，2008年，国产动画产量达到13.10万分钟，2009年，这个数字是17.16万分钟。<br />
<br />
“短短的六年时间，国产动画的产量突飞猛进40倍”，金德龙表示，而且随着产量的增加，我国动画质量明显提高，涌现了一大批优秀的国产动画片，《美猴王》、《喜羊羊与灰太郎》、《哪吒传奇》、《西游记》等一批为人所称道的作品，一些优秀动画片开始走向海外。<br />
