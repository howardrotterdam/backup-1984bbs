													<h2>对《尚不能判断重庆涉黑案辩护律师造假》一文提出的三个疑问</h2>
						
						
																											<div id="postmessage_376519" class="t_msgfont"><a href="http://say.cqnews.net/plzl/200912/t20091215_3875238.htm" target="_blank">http://say.cqnews.net/plzl/200912/t20091215_3875238.htm</a><br />
<br />
2009年12月15日 19:48:21&nbsp; &nbsp;华龙网<br />
 <br />
　　随着重庆打黑行动的步步深入，一个个惊心动魄的内幕被一层一层的揭开，而涉及到的人员遍布各个行业。而最近重庆市内掀起的“律师造假”门更是涉及到了公众最为敏感的行业：律师行业。而央视《新闻1+1》针对这次“律师造假”门特别作出了一期专题节目。笔者在仔细观看节目数遍之后，对该期节目有几个疑问，在此借网络一一提出： <br />
<br />
　　一．首先就是主持人提出的第一个问题：“您（何兵）觉得现在就用这个“律师造假门”时机是不是恰当的？”何教授的回答是：“我觉得用词是不专业的。”这里可以看出，问题强调的是“时机”，而何教授的回答却是用词不专业。笔者在这里可以将何教授的话理解为：在当前情况下，不应当用肯定语气的“律师造假”，而应当用“律师涉嫌造假”。这样的用法既可以明白无误的告诉公众该律师目前只是被怀疑涉嫌造假，也透露了我们的司法机关是有证据来证明该律师确是“涉嫌”的。而《新闻1+1》栏目开场就提出这么一个问题的做法让笔者百思不得其解：我们暂时当做是对媒体的报道是否符合事实提出质疑，防止舆论一边倒从而影响到司法公正。但笔者却注意到节目开始播出的是该消息是由重庆市政府新闻办发布的，这里我就想问主持人，根据新闻的特点：真、实、强、短、快、活，你面对的是一条由政府专职发布新闻的机关所公布出来的新闻，你首先想到的不是政府是否有证据来支持这条新闻的真实性和正确性，而是直接站出来指出政府的行为没有遵循上述新闻特点，在说“谎话”。作为国家级的媒体，这种严重不负责的、草率的行为会对行政机关、司法机关的公信力造成不可忽视的影响。所以我不得不提出自己的疑问：“你一开始就把矛头对准了我们的政府，是不是也在制造舆论，把焦点转移到政府上，使大家觉得该涉案律师是被“冤枉”的，没有“造假”? <br />
<br />
　　二．然而，就是在主持人开门见山的“引导”之后，紧接着何教授用两个极片面的解释，最终给我们的司法机关公布消息的行为“定性”为不符合事实、不符合时机。这种做法，是在引导公众把注意力转移到政府和司法机关的公信力上，而不是去关注案件的本身，这势必会影响到司法机关将案件按照正当的法律程序运行。 <br />
<br />
　　就是在这样的主持下，节目进一步播出周立太律师、赵长青教授等人的资料短片，然后通过何兵教授的点评，逐步的把焦点引到了政府、司法上面，从而让人感觉到这次“律师造假门”是不是一桩冤案？然而，何教授在针对当事人选择律师时的标准作出讲解的时候，提到了当事人选择人脉关系广的律师是司法和律师队伍的一些不正常情况的体现。这里，我又觉得很奇怪。当谈到律师队伍不正常情况时，何教授只是蜻蜓点水般一带而过，不是就此展开“不正常情况”这个话题，向整个律师队伍的阴暗面开火，而是马上提出律师的影响可能会对公正判决有好处。笔者再一次疑惑了，你为什么没有说出律师会对司法机关作出公正判决产生负面影响？如此牵强、有失客观的表述，实在让我越发感觉到这个节目是在将公众的注意力引向政府、司法机关长期以来被质疑的问题，而忽略了案件本身，从而为涉案律师“开脱嫌疑”。 <br />
<br />
　　三．在主持人针对赵长青教授因给犯罪嫌疑人作辩护，遭人误解被骂而提出问题时，何教授提醒公众将心比心，假设自己或自己的亲戚涉案了，希望得到一个有能力的律师的帮助。笔者再一次提出疑问：主持人的问题针对的是赵长青教授被误解，而何教授的回答却是针对当事人。笔者奇怪了，这是何教授没听清楚或者没听懂主持人的问题？显然是否定的，作为中国政法大学法学院副院长的何兵教授，以他一个成年人的行为能力和其法学底蕴，显然是听得清并且听得懂主持人的问题，但为什么何教授偏偏是为当事人做解释。这点，我实在无法接受。节目的前半段，何教授的表述不断的在向公众作出一个观点：那就是律师是提供法律服务的职业，它所提供服务的对象是不分原、被告与“好”、“坏”的。人们要理解律师这个行业，不能把对“非正义”行为的惩罚要求强加到公正立场上的律师行业，不允许其为犯罪嫌疑人提供法律服务。而何教授针对赵长青教授被误解这个事实，作出的却是牛头不对马嘴的解释，我深感遗憾。赵长青教授作为刑法学界的泰斗，而何教授作为同是法学教授、同是研究刑法学的学者，同是提供法律服务的律师，却在中央级媒体上不但没有对同行作出客观的、正确的评价，反而任由其被误解，这不得不说是一个遗憾。 <br />
<br />
　　结尾处，笔者想说的是，在事实没有查清、证据没有查证之前，请公众不要胡乱猜测、片面地批判我们的政府、司法机关、法学学者、律师；我们的媒体也不要为了迎合各方心意，刻意制造有失公正的舆论导向。总之，真相只有一个。<br />
<br />
(文/心境)</div>

							
							
							
							
