&nbsp; &nbsp;该文还称：“李庄，48岁，混迹律师界十余年，其所在的康达律师事务所在京城也颇有“背景”。注重“身价”的李庄此次肯来重庆打涉黑官司，除受龚刚模的生意伙伴相邀答应来“捞人”，其实更重在“捞钱”。”对一个执行辩护职务的律师，在没有司法定性前，这个记者很无知地已经对他定性为是“混迹律师界十余年”，是“为捞钱”，是“有背景”。已经将其媒体批判为一个混混。<br />
<br />
第二，&nbsp; &nbsp;以偏概全，恶意贬损中国律师整体形象，对所有到重庆辩护的律师贬低为同小姐一样“人傻、钱多、快来”的钱者。<br />
<br />
该文称：欣喜之余，李庄向京城同行发出信息：“够黑，人傻，钱多，速来！”“一位不愿意透露姓名的重庆政法干部告诉中国青年报记者，重庆打黑除恶一系列案件进入司法程序后，“到重庆代理涉黑诉讼”一时成律师界热门。许多北京律师如赶场般云集重庆，寻找开展“业务”和施行“潜规则”的机会。”将有一万六千多律师的北京律师群体，描绘成一群如蝇逐臭的唯利是图者。对中国律师业既无知又妄加评论，认为律师为保护被告基本权利的工作，都是为了捞钱。<br />
<br />
第三，&nbsp; &nbsp;全面否定中国刑事辩护制度，称95%刑事辩护是无用的。暗示中国百姓没有必要请律师，请律师是受“第二次伤害”，化冤枉钱。甚至称律师是“国家和民众的灾难”。<br />
<br />
该文称：“据资料，在刑事案件中，律师胜诉的比例仅有5%，也就是95%是败诉。“面对当事人的巨大诉讼投入，有多少律师在说明败诉原因之余会对当事人说‘对不起’？当事人有苦难言，实际上造成了‘第二次伤害’。律师的尴尬作为和滥用‘潜规则’，所造成的灾难全由国家和民众来承受。”<br />
<br />
刑事辩护的胜诉率，是这个记者和新闻来源者的十分荒谬的“发明”。可以负责地说，“刑事败诉率”，是这位无知记者的捏造和杜撰，中国最高法院、司法部、全国律协、国家统计局，从来没有这种数据统计和所谓的“资料”。因为“胜诉率”是有多种参数影响的抽象概念，根本无法统计。刑事辩护的功能，是保护被告作为一个人的基本权利，是人类文明的重要进步，提高司法公正性，防止冤假错案发生。这位记者和新闻提供人的法律意识，还停留在封建社会。以死刑案为例，杀错一个都不行，如果按这种“比例法”，难道杀错5%都是可以的？这位记者知道中国一年有多少死刑吗？95%败诉，如果是指有罪判决都算败诉，那么排除情节、减轻情节和帮助法庭准确定罪量刑，难道就是无效辩护？有罪判决责任就是律师？佘祥林冤案是谁造成的？聂树斌是谁错杀的？难道要律师说对不起？说律师辩护是“第二次伤害”，象土改一样抓起来不用审判就枪决，象文革一样不经审判就定国家主席是大叛徒大内奸大工贼，看来是这位记者期望的。说律师“所造成的灾难全由国家和民众来承受。”不用说法律水平，这位记者的现代人文意识，连中学生都不如。这样的文章，中青报能够出笼，确实体现了中青报的堕落。<br />
<br />
第四，&nbsp;&nbsp;违法透露侦查内幕，将侦查观点渲染成定性事实。充满有罪推定的“专政观念”。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;《律师法》规定，律师会见权不受侵犯，会见不被监视。但是，从这位记者的报道中，李庄律师办案中受监视的迹象非常明显。该文说：“其实，在龚刚模按响报警铃之前，律师李庄等人违法操作、妨碍正常司法的行为已引起相关部门的警觉，巡查民警多次批评和警告，李庄仍置若罔闻，看守所依法作了详细记载。”看守所无权监视律师，不得干扰律师的正常会见工作。这个“相关部门”已经是直接违法的行为，被这位记者报道成正面行为。<br />
<br />
为了涂黑律师，这位记者违反法律规定，将侦查内幕未经审判大量公开。称涉黑被告的翻供是一种醒悟，是检举立功。将其未质证也无法去澄清核实的口供，进行所谓的“公开”：“龚刚模说，李庄为我打官司就是为了钱，最终处理结果还得落在我身上。官司是否打得赢，他都出名了，如果被查出我作了伪证，倒霉的还是我自己！”说律师引诱要被告翻供：“李庄在第二次会见中对龚说：从你的材料中看得出来，你肯定被诱供和刑讯逼供了。法庭上问你是否被刑讯逼供时，你要大声承认，还要把刑讯逼供的过程夸张地演示出来，以刑讯逼供为由否认原来在侦查阶段所作的口供。同时，李庄还编造了一大堆细节，要求龚刚模在法庭说：以前的口供全是瞎编的，因为我被公安吊了八天八夜，被打得大小便失禁。李庄用威胁口吻告诉龚刚模：如果依照刑讯逼供所说的笔录就得枪毙你……翻供你要有道理，有理由。“面对专案民警，龚刚模发泄般吐露几天来煎熬着他的秘密：他妻子从北京请来了康达律师事务所律师李庄、马晓军。在与李、马二人的前后3次会面中，李庄向他面授了五招翻身秘术。”<br />
<br />
这位记者不明白，他这样单方报道时，李庄和龚刚模都关在里面，丧失了正常的话语权，不象法庭上可以澄清、质证和辩驳。这种报道是“专政报道”的常用手法，是违背基本的新闻公允原则的。被告口供，特别是检举立功材料，是侦查机密，侦查人员无权在开庭前透露，连律师都无权透露，记者怎么可以这样泄露报道？谁知道龚是不是这样说、有没有说过、是什么样的情况下说、其动机又是什么？这种“曝光”，除了证明办案机关急于定性和引导社会舆论的怂恿和授意外，这两位记者的身份和意图，不是昭然若揭？<br />
<br />
第五，&nbsp;&nbsp;将违反《宪法》和《刑事诉讼法》的联合办案，视为正确做法，是非观念混淆，对公检法联合办案对付毫无防卫能力的律师，进行正面鼓吹。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;该文称：李庄、马晓军等律师教唆龚刚模翻供、串证等问题一露端倪，立即引起了重庆打黑领导小组的高度重视，迅速组织公、检、法、司人员成立联合调查组。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;我国 《宪法》规定，公安机关依法侦查、检察、法院依法独立检察、审判，不受任何单位和个人违法干预。法院都介入“联合”，还要走审判程序干什么？这是中国法治的倒退和旧法观念的回潮。自从中央政法委宣布结束“严打”进入常态后，“公检法联合办案”已经是明确不允许的。政法各家要各司其职，互相监督制约，各自发挥职能作用。我国刑事诉讼法解释的的刑事侦查分工，伪证罪是公安独立侦查。重庆的案件，公开宣称“组织公、检、法、司人员成立联合调查组”来调查两个律师的帮助伪证问题，是不是太小题大作了？充分体现了一种非常态和不依法办事的现象。把依法辩护的律师视为阻挠打黑的恶势力，用对付黑社会的手法来对付律师，这两位记者不以为非，反而津津乐道。<br />
<br />
第六，&nbsp;&nbsp;非议诋毁和否定全国人大新立法的《律师法》，为一些落后的旧法观念招魂。<br />
<br />
该文为了否定律师在刑事程序中的作用，竟然认为全国人大已经通过、国家主席令公布的《律师法》是不合时宜的，影响了重庆打黑。他们说：“李庄现象”泛滥的背后，是“潜规则”还有其存在与蔓延的空间，一种原因是我国《律师法》相对超前而其他法律相对滞后。”这种论调，是立法争论中一些淘汰旧观点的泛起，也是《律师法》通过后遇到巨大阻力，一些基本律师权利严重被搁置的主要原因。稍知道立法过程的人都清楚，我国现《律师法》已经是非常保守的一种定位，同中国已经加入公开承诺遵守的国际人权公约还有不少距离。而该文借重庆“打黑”之机，反而再次鼓吹那些落后观念，非议少得可怜的律师法中的些许进步。<br />
<br />
第七，&nbsp;&nbsp;将法律规定的律师正常法律帮助工作，理解为帮助翻供，将律师正常调查，理解为串供。误导刑事诉讼基本常识。<br />
<br />
从报道所称，李庄等律师介入本案是审判阶段。该阶段侦查已经完毕，口供证据已经固定，法庭上怎么说，都不可能影响已经固定好的笔录。审判的作用，是在法官主持下，对这些口供进行质证核实，还原真正的真相。所有的口供，质证时都会有同意、确认、纠正、澄清、辩解、否定。这些都是庭审的基本程序，也是庭审的意义所在。因此，庭上翻供不构成伪证。审判阶段律师会见就是要对控方证据进行核实，对各被告口供，向被告进行核实鉴别。对律师将进行的的证据调查思路、已经取到的证据、证据的证明内容、证据的线索，向被告进行一一询问，包括告知已经有的证据中的矛盾和证明力。一些公安机关滥用权力，审判阶段还监视律师，不让其向被告核对和告知，这是违法的、侵越辩护权的。这位记者盲从了这些违法观念，以为这是“串供”。该文说：“李庄在首次会见龚刚模的过程中，即向龚刚模宣读同案多名犯罪嫌疑人的笔录材料，特别是宣读了同案另一主要犯罪嫌疑人樊奇杭的多份交代笔录，同时还把同案重要嫌疑人的在逃信息告诉了龚刚模。”他不知道此时侦查早已结束，律师这样做是合法的。但经过他的这种报道，给社会上包括一些侦查机关产生一种误导，以为这真是一种伪证和串供。<br />
<br />
口供雷同，本是刑事律师审查定罪证据是否合法取得、是否原始取得的基本方法，也被这位不懂刑事辩护的记者非议，按控方观点指责和辩解说：“但是经过司法调查（该记者对何为司法亦没有搞懂，将刑事侦查说成司法调查），李庄的种种造假设计经不起推敲，其“刑讯逼供”和“无法正常会见”等种种说法不攻自破。”“为设置更多障碍，李庄不断炮制出新的质疑，如检察机关移送证据不足、龚的交待笔录出现多份雷同等。”“检察机关移送的主要证据复印件，与拟在法庭举示的证据内容基本一致，符合《人民检察院刑事诉讼规则》关于移送主要证据范围的规定；而笔录雷同系多位侦查员在不同时间对龚进行讯问，其内容不可避免会出现相同或相似。”把律师正常的履行职责行为，检察院的不严格按刑诉法、律师法保障律师阅卷权的行为，编排成是律师在刁难检察院，并进一步认为是一种伪证、捞钱的手法。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;八、大肆渲染律师“捞钱”，闭口不谈有无合同、有无私下收费，渲染律师都在唯利是图。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;该文中多处反复渲染律师的大规模“捞钱”。是煽动社会敌视律师的“最有杀伤力”的一招。在其报道中，没有一个到重庆的律师不在巧取豪夺。不交待有没有协商和合同依据，当事人自愿还是被要挟、被骗。报道称：“龚刚模的亲友“捞人”心切，再加上李庄的多番“演说”，几天之内就总共支付了245万元给“跨区域打捞队”。李庄代表“打捞队”要龚刚模的亲友承诺：若要龚刚模不判死刑，还要两三千万元，事成之后兑现。”如果此说如实，李庄涉嫌的就不是帮助伪证罪。而是违反执业纪律、违反律师法的行为，应当受行政处罚和惩戒。如果虚构事实骗钱，还构成合同诈骗罪。但是，这篇报道没有交待这些费用是辩护费还是其他代理费，是律师提出还是家属自愿，是进律师所的帐还是自落腰包。而是笼统地用讥讽口吻说：“迫于无奈，龚云飞又托人再次给李庄的账户“装”了100万元。”好象律师收钱就是犯罪。重庆案件中，律师和“钱”似乎结下不解之缘，如沸沸扬扬的女律师和法官勾结进帐4千万之类。我们不排除律师队伍中确有唯利是图者，严重违规高标准收费者，但此文的渲染，似乎中国的律师都在不顾公义和道德趁火打劫，把律师涂抹成比黑社会还坏。这种无知和偏见，在一个中央的青年大报上出现，体现了中国现阶段法治意识的倒退和观念的混乱。中国青年报如果是一份有责任感的报纸，应当郑重向全国律师界公开道歉.</div>

							
							
							
							
