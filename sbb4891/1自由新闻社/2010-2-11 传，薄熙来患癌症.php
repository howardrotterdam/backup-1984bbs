<br />
<br />
2010年2月9日 星期二<br />
 <br />
薄熙来癌症突加重<br />
 <br />
姜平<br />
 <br />
【人民报消息】薄熙来在2009年夏天带头大唱红色歌曲，这次活动中，薄熙来偶然发现自己得了喉癌。虽然他积极治疗，并竭力掩盖，但没有不透风的墙。<br />
中央并没有因为薄熙来为党歌功颂德而放松对他野心的警惕和监控，2009年9月，上面做了一个大动作，让公安部经济犯罪侦查局处长周京平紧急空降重庆，挂任「重庆市公安局经济犯罪侦查总队」党总支书记、总队长。去了连个「代」字的缓冲都没有，直接就担任一把手、就说了算，这在中共官场上还没见过。<br />
<br />
任务紧急到什么程度？都没让周京平回家拿身换洗的衣服，首都机场买不到此次航班的票，就飞车去南苑机场。<br />
<br />
被重庆官场称为「王阎王」的王立军神情紧张的去找薄熙来，问上面的态度。这让薄熙来不痛快了好长时间，对胡温和习李的恨更深入骨髓。<br />
<br />
据2009年10月的一项调查表明，医学专家正在达成共识，癌症治疗的最终目的是尽可能的延长患者的生命并维持及改善患者的生活质量，通过阻碍或延缓癌症的进展以实现对该病的长期控制。<br />
<br />
要达到这个目地，不但医生要给予治疗，最重要的是癌症病人本人必须配合，才能实现。想长寿最基本的一点就是与世无争、心平气和、精神愉快。这三条正是薄熙来的致命处。<br />
<br />
按理来说，得了癌症，薄熙来就应该把抢占胡锦涛位置的心收一收，全党算起来，此届享受中央政治局委员待遇的只有25人，享受直辖市市委书记待遇的只有4人，在这个位子上还不知足吗？况且还得了癌症。<br />
<br />
「薄熙来已经死到临头」！这是那些被他整治的人咬着牙根子说的。<br />
<br />
确实「薄书记辛苦了」。在重庆「打黑除恶」，即使薄不亲自出马，也得亲自筹划和指挥整治哪些人，而且这些人不是「一小撮」，而是一大堆。「打黑」，去年竟把39岁的奉节刑侦队长帅辉累死在11月28日。帅辉没有心脏病，心脏竟能累出病来，可见薄熙来下手有多狠。<br />
<br />
近来，让薄熙来癌症加重的事情不止一件，例如新任辽宁省委书记王珉奉胡中央之命，抄了他的后路，不仅撤换了大连公安局长，由省厅副厅长接替，而且改任了大连检察院的检察长。辽宁省的老对立面们也积极往薄熙来眼睛里撒胡椒粉。控告的罪犯除了他的亲信死党外，还包括他老婆谷开来。<br />
<br />
这些薄熙来都知道，他唯一能让中央没辙的，就是在重庆唱红唱的更响，以此制造新闻，吸引人的眼球。<br />
<br />
2010年1月16日上午，薄熙来与精心挑选出来的来自全市27所高校的近300名学生「代表」见面，薄熙来说：「看到你们，就让我想起小时候的事儿。人在年轻的时候会有很多梦想，想自己是八九点钟的太阳，想到了十一点会怎么样，十二点又是个什么样子？未来，我们一定是sunshine（阳光灿烂）！」<br />
<br />
结果，回去以后，打算在中共中央首席灿烂的「薄阳光」就癌症加重了。癌症复发，并向其它部位转移，还有没有未来，没有人不知道。<br />
<br />
