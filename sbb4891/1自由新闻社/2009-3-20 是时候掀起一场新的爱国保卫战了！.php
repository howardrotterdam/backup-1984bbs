不是中国人用不着看 2009-03-21 18:30 | (分类:默认分类) <br />
这篇日志不是为了娱乐，不是为了点击率，只因为我是一个中国人！<br />
<br />
大家都知道校内素来以清洁的网络环境,高雅的思想内容而受到了广大学生朋友们的青睐,可谓是当今网络世界里珍贵的一方净土。这也正是校内网区别于猫扑大杂烩等娱乐性质网站的地方。<br />
<br />
然而。。。。<br />
<br />
最近我却在校内发现了一些令人发指的肮脏内容：不是暴力凶杀色情，而是――――台独势力。<br />
<br />
最近校内惊现一群台独分子，他们有的是台湾本土的年轻人，有的是台湾当局台独势力的行政人员，甚至还有少数居心叵测，丧尽天良的大陆受台独势力利用的。。。人？我恐怕很难用这个词。<br />
<br />
我加了其中一个气焰最为嚣张的人为好友，我研究了其留言记录，状态记录，收藏。。。。 对这些人的惯用伎俩知道了大概： 他们多注册为大陆的大学（一般不是名牌也不是很差的那种），少数直接注册台湾，之后会主动地加大陆的学生（主要是大学生）为好友，并且使用留言，状态，收藏等一切可能的途径散布政治谣言，进行政治煽动。被加为好友的大陆学生通常有两种情况：相当一批学生能够时刻维护国家利益与民族团结，毫不客气地进行反驳和斥责，令我痛心的是，也的确有一批学生被台独势力别有用心地思想控制，体现了某种程度上的动摇，更有甚者，公然与台独分子高谈阔论，俨然已经同流合污。。。。。<br />
<br />
我的朋友应该都知道，我并不是愤青，在政治上我一贯以尊重客观事实，实事求是为原则。但这次我火了！我是个中国人！台湾问题是历史遗留问题，现今的形势决定了其具有一定的自主权，像组团参加奥运，有自己的护照等，我认为这都是可以理解的，这些极端台独分子竟散布一些荒谬至极的的不负责言论：什么“大陆属于中华民国”，“非法政府”等等。。。。。。其极端程度闻所未闻，还恶意攻击我国政府，我看了心里十分难受。我们毕竟都是炎黄子孙，我们都是中华民族，这样的分裂祖国的言行必将受到每个中国人唾弃。<br />
<br />
我自然去骂了几句，后来就是对骂。但事实上不是每个中国人尤其是年轻人都很清醒，已经有人通过校内网这一渠道在思想上受到了蛊惑，校内网上都是年轻人，而年轻人必将是祖国发展的中流砥柱，这些台独势力挑选校内网为其另一个活动途径其用意可谓险恶！！！<br />
<br />
下午原本一直在校内闲逛，突然觉得自己真的有责任写点东西，提醒所有高中生，大学生： 我们一定要坚定立场，同分裂国家统一，尤其是破坏民族团结的行经做斗争，以后见到类似言论，坚决与之斗争，一定不能与之同流合污，一定不能让台独势力的阴谋得逞！<br />
<br />
希望大家分享这篇日志，让更多人看到。我要让那些台独分子知道：<br />
<br />
中国人是多么的团结一致！中国人是多么的立场坚定！中国人是多么的热爱我们的祖国！<br />
