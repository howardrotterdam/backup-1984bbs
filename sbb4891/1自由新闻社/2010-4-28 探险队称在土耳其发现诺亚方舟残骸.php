													<h2>探险队宣布在土耳其发现诺亚方舟遗迹</h2>
						
						
																											<div id="postmessage_588247" class="t_msgfont">国际在线报道 (记者 侯一冰)：诺亚方舟的故事想必大家都不陌生，故事出自《圣经》，传说是这一个方形船只，其建造的目的是为了让诺亚与他的家人，以及世界上的各种陆上生物能够躲避一场上帝因故而造的大洪水灾难。诺亚方舟虽然是个传说，但由于《圣经》中记载的很多事情都被证实是真实的，所以，很多年以来，许多国家的圣经考古学家都希望揭开这个千古之谜。<br />
<br />
　　28日，一支由香港人和土耳其人组成的探险队宣布，他们日前在土耳其东部的阿勒山海拔超过4000米的地方发现了诺亚方舟遗迹，并成功进入巨型木结构的方舟内，探险队员还在方舟内发现了陶器、绳索以及类似种子的物体。<br />
<br />
　　探险队称，他们已对遗迹进行了碳元素鉴定。结果证明此次发现的遗迹可追溯至4800年前，这个时间也正是《圣经》中所提到的诺亚方舟的年代。荷兰著名方舟探索家格力特－艾顿介绍说，这次发现的诺亚方舟遗迹有非常多的细节与历史记载吻合：方舟在山上是轻微倾斜、而且是棕红色的木头、颇长的长方形，艾顿称，已有大量的证据表明，这个木结构就是《圣经》中记载的诺亚方舟遗迹。<br />
<br />
　　土耳其亚勒省文化局局长米辛－布卢表示，两千多年以来，历史记载及目击者证据都指出，有一艘古老的大船避过洪水而停泊于阿勒山上，人们相信那是诺亚方舟。而这次探险队发现的这个长方形木结构，以及初步的科学研究，我们相信这就是历史记载中的诺亚方舟遗迹。<br />
<br />
　　然而，对诺亚方舟遗迹的发现持怀疑态度的人也不在少数。土耳其许多专家学者都对诺亚方舟的真实性提出了质疑，还有一些学者认为，方舟的发现地并不在阿勒山，探险队宣布的诺亚方舟的出现时间也并不准确。一周前刚刚卸任的阿勒省省长迈赫迈德－切廷指出，探险队2007年和2009年进行的关于诺亚方舟的发掘工作并未得到阿勒省的允许，他称如果当时知道的话一定会干预此事，此次探险队宣布的事情应该是他们2007年的探险结果。<br />
<br />
　　如此看来，究竟历史上诺亚方舟是否真正存在还有待有关方面进一步挖掘。<br />
