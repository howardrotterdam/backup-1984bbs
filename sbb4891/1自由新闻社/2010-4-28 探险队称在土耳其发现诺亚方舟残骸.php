发现的空间结构示意图<br />
　　历时接近2年的探索中，香港队员李耀辉以先锋队员身份，核实巨型木结构位于海拔四千米以上，及掌握地理环境，为进一步探索做准备。李耀辉在发布会上表示：“我在2008年10月跟随土耳其队员攀登亚拉腊山，当时攀登4000米以上的高度，看见由粗木结构，而且还有入榫位，证明是古旧、没有采用钉的建筑。在结构附近100米处，有散落的木头，穿过冰川，长度达20米，由环境看来，木结构是长年被冰和火山石覆盖。在我出发前，土耳其的队员先在山上凿冰，令本来掩埋的结构部分暴露出来。”<br />
<br />
　　队员所发现的木结构已经破损，并从不同的破口进入，至今为止共发现七个空间，队员杨永祥在会上根据视频资料进行了详细解说，与会的专家及政府官员均认同此次的重大发现。从历史记载考证，他们相信有关木结构的最合理解释为挪亚方舟，有助积极做进一步的科学研究。荷兰著名方舟探索家格利特・艾顿指出：“此次发现的重要性在于，是首次在历史上具体拍摄来了方舟的发现，并向全世界揭示。”<br />
<br />
　　土耳其考古学家阿密・乌兹伯博士解释山上环境有助保存木结构数千年而没有石化或腐烂。他说：“目前，亚拉腊山的永久雪线位于海拔3900米，而木结构在4000米以上。”山上的低温及地理环境，包括冰川沉积物及火山物质有助结构保存，另外，他又解释木材可以负载本身重量的5倍，因此结构可以承受巨大的重量而没有粉碎。<br />
<br />
