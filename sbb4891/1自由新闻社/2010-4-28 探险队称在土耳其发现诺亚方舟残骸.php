													<h2>探险者称在土耳其东部发现诺亚方舟残骸</h2>
						
						
																											<div id="postmessage_769605" class="t_msgfont"><img src="http://www.qiuzhang.net/img/2010/04/D911478671.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
根据中土联合探险队公布的照片，一名队员正在查看一座木制结构的部分，他们宣称这一结构可能是亚拉拉特山存在诺亚方舟的证据。目前尚未有该地点的外部照片，探险队表示，在土耳其政府将其指定为考古遗址以前，他们不会公布发现疑似诺亚方舟的准确地点 .<br />
<img src="http://www.qiuzhang.net/img/2010/04/A011471770.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
照片中是山顶覆盖积雪的亚拉拉特山。探险者称，他们在亚拉拉特山约3960米海拔处发现了诺亚方舟残骸 <br />
<img src="http://www.qiuzhang.net/img/2010/04/AD11471717.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
这张由探险队公布的照片显示了一座木构造建筑的部分墙壁。一名队员表示：“虽然我们不能百分之百确定它就是诺亚方舟，但可能性却达到99.9%。”<br />
<img src="http://www.qiuzhang.net/img/2010/04/B1114712604.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
2006年，美国国家安全分析师波切尔-泰勒宣称这张卫星照片上显示亚拉拉特山西北角有“异常物体”<br />
<img src="http://www.qiuzhang.net/img/2010/04/A6114713273.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
他认为那就是诺亚方舟的残骸 <br />
<br />
　　这是探险队员宣称在亚拉拉特山附近发现的船体木梁。数百年来，基督教徒、犹太教徒和穆斯林一直对寻找诺亚方舟残骸十分着迷。虽然许多人宣称找到了诺亚方舟，但他们都没有提供任何科学证据。 <br />
　　据国外媒体报道，中国香港和土耳其的探险队员表示，他们在土耳其东部的亚拉拉特山附近找到了传说中的诺亚方舟的船身残骸，测试发现这些残骸的年代可以追溯至4800年前，即《创世纪》中所描述的诺亚方舟的存在时期。<br />
　　香港导演杨永祥(Yeung Wing-cheung)说：“虽然我们不能百分之百确定它就是诺亚方舟，但可能性达到99.9%。”杨永祥与另外15个福音派教徒组成的探险小组对这座土耳其最高峰进行了积极探索。据该探险小组介绍，他们在亚拉拉特山附近积雪和火山残骸下面找到了七个大木舱，那里的海拔超过1.3万英尺(约合3960米)，对取自该处的一块木料的测试发现，它的年代可追溯至公元前2800年。 <br />
　　如果最终被证实确是诺亚方舟，这将是考古学历史上最重大的发现，同时还能证实《圣经》中最著名的故事之一。探险队成员都来自香港诺亚方舟国际事工组织(Noah’s Ark Ministries International)。他们表示，2009年10月其在亚拉拉特山发现了诺亚方舟残骸，亚拉拉特山也是《圣经》中所描述的诺亚方舟最后停泊处。 <br />
　　探险队4月27日召开新闻发布会，宣布了这一消息。小组成员李博宇(Panda Lee)说：“我看到了一个用木板一样的木料建造的结构。每块木板大概有8英寸(约合20厘米)宽。我能看到榫头，这是金属钉使用之前的古代建筑的证据。我们步行100米到达另一个地方，我看到了嵌入冰川的断裂木头片段，大约有20米长。” <br />
　　探险队称，整个木结构有多个船舱，一些还有木梁，其中一个船舱的木墙很光滑，稍显弯曲，他们公布的录像显示里面有门、楼梯和钉子。他们表示，木料看上去取自柏树，而据《圣经》描述，诺亚方舟是用“歌斐木”(一种尚未确认的树木)打造。探险队排除了木船是人类居民点的可能性，并解释说迄今从未在如此高海拔地区发现这种居民点。他们未公布发现疑似诺亚方舟的准确地点。<br />
　　4年前，美国国家安全分析师波切尔-泰勒(Porcher Taylor)经过十年的研究，宣布卫星照片显示亚拉拉特山西北角有“异常物体”，他认为那就是诺亚方舟的残骸。不过，英国考古学家迈克-皮特(Mike Pitt)表示，中土联合探险队尚未提供任何有说服性的证据。他说：“如果那里发生过洪水，能在4800年前将一艘巨轮升高至山腰，那我认为世界上可能存在这种洪水的地质证据，问题是没有。” <br />
　　英国牛津大学古代史讲师尼古拉斯-普塞尔(Nicholas Purcell)表示，最新研究其实是“陈词滥调”。他说：“如果公元2800年欧亚大陆被三千多米深的洪水所覆盖，已经有数百年历史的埃及和美索不达米亚文明如何在这种条件下生存?”<br />
　　根据《圣经旧约-创世纪》记载，诺亚方舟是一艘根据上帝指示而建造的大船，上帝希望发洪水淹没世界，以惩戒那些罪恶之人。这一故事被普遍视为事实，直到19世纪，科学家开始对这种全球性洪水的证据提出质疑。</div>

							
							
							
							
