　　新浪科技讯 北京时间4月28日消息，由香港人和土耳其人组成的方舟探索队今日在北京<br />
新浪科技讯 北京时间4月28日消息，由香港人和土耳其人组成的方舟探索队今日在北京举行发布会，称于土耳其东部的 <br />
 亚拉腊山寻找挪亚方舟遗骸时，在高于海拔四千米的冰川进行挖掘，成功进入巨型木结构。木样本经科学分析，证实有4800年历史，土耳其政府及文化部官员与香港探索队员联合公布探索成果，包括图片及影像资料，拟将来把发现申请列入联合国文化遗产，并签署共同合作协议，向香港队员颁赠荣誉市民殊荣。<br />
<br />
　　此前西方探索家过去的最大成果，是在亚拉腊山四千多米的冰川上，找到零碎的木块。从客观科学分析，科学家一致认同高地上没有树木生长，由于气候严酷，三千米以上亦没有人类建屋聚居多的遗迹，加上当地世代沿袭以泥砖建屋，木材是罕有建材，因此，过去木得发现，都推论为可能是来自方舟。<br />
<br />
　　此次在探索上的重大突破，首先是锁定位置，在四千米以上的冰川进行挖掘，成功发现并进入木结构。挪亚方舟国际事工总干事袁文辉在发布会上指出：“我和众探索队员亲身进入亚拉腊山上有间隔的人工木结构内，而我们相信进入的木建筑就是历史文献和当地人一直所指的古木船。”队员再结构内进行量度、样本收集及拍摄了整个过程，这个发现，是全球首队探索队以影像清晰记录了山上人工木结构的内部空间。<br />
<br />
　　探索队领队阿密・埃图鲁最先掌握位置并开始探索，他解释道：“我在2008年6月知道秘密地点，消息来源指向那是挪亚方舟。我带领队员在那一带探索并发现木结构。我拍摄了内部空间照片。”<br />
