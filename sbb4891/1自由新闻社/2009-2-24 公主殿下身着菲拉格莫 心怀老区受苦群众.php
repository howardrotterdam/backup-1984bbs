薄瓜瓜-当选中国时尚先生<br />
<br />
薄瓜瓜<br />
<br />
薄熙来的儿子薄瓜瓜现在正在牛津读书，年纪轻轻的他已经当选为中国时尚先生。<br />
和想象中一样，英国正是那种阴霾郁结的天气，雨常常冷得让人想到这里是异乡。但薄瓜瓜对此很满意。对他来说，这就是最理想的天空。<br />
有时候未来会在一瞬间打动你。虽然你并不知道那是什么。12岁那年薄瓜瓜打定主意要到英国来上学，那是他第一个改写未来的决定。在北京，他甚至不肯学英文课，因为老师的发音在他看来很不标准。而习惯自己作出判断和选择，是80年代生人的一个标志。<br />
2006年9月，瓜瓜将进入牛津大学就读PEE（政治经济哲学）专业。中学时代结束了，一个少年也将代之以对世界开始发言的青年。即使在相对还封闭些的中国，这个年龄的年轻人已经在灵光闪动，薄瓜瓜在英国，他写了一本书，《Uncommonwealth》，来预告自己的出现。以英文、插图、诗句、宣言构造而成的实验品。<br />
几乎没有几个中国孩子能在十几岁时打上学校橄榄球队的后翼，因为似乎没有一个校长喜欢看自己的学生在草地上打群架一样滚来滚去，所以瓜瓜抱球触线那一刻的感觉，别人不太容易感同身受。同样地，他还是学校马术队队长这件事，对于国内的同龄人也很陌生。他会在讲出准确的形容词时伴以无声的响指，来强调话语节奏，他会跳街舞，对探戈大赞其酷，他会告诉你在看电影时他已经想到导演是如何调配现场，摄影镜头如何运动之类，但马上他又会说技术其实并不重要，眼和心的敏感才最难达到。<br />
有人担心他变成一个英国绅士吗？他也曾在云南大理一个人背包旅行的途中，见到那些穷苦的人，他们生活中唯一的乐趣是看看电视，从那些小孩子眼睛里看不到太多意识流动。他喜欢中国古代道家的《易经》，也包括陀思妥耶夫斯基的《白痴》，他好奇上帝是否存在，当然，他很肯定地认为并没有一个白胡子老头在某处担任上帝。他认为数学也是一种文学，它有多少理科的特性就有多少文科的特性――“从前的哲学家都是数学家啊！”</div>

							
							
							
							
