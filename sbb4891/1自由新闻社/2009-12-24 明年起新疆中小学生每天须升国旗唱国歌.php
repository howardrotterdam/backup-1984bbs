													<h2>明年起新疆中小学生每天须升国旗唱国歌</h2>
						
						
																											<div id="postmessage_393369" class="t_msgfont"><a href="http://news.163.com/09/1224/16/5RAHLIN70001124J.html" target="_blank">http://news.163.com/09/1224/16/5RAHLIN70001124J.html</a><br />
新华网12月24日报道 从2010年1月1日起，新疆中小学校每天都要举行升国旗、唱国歌仪式，并作为制度长期坚持。<br />
<br />
新疆维吾尔自治区教育厅要求，由于天气等自然原因影响而不能在室外举行升国旗仪式，由班主任组织学生在教室进行，学生可对着教室墙上的国旗行礼。中小学所有教室内、教学楼醒目处都要悬挂中国国旗，张贴中国地图。教育厅选择了50首左右的优秀爱国主义歌曲，制作成光盘下发到全区每一所学校。各学校要开展教唱红色歌曲活动，通过影院、远程、视频等形式每个月组织学生观看两次红色电影。<br />
<br />
自治区教育厅还要求，每个学生一学期阅读爱国主义题材的书籍不少于1本；每学期布置不少于一篇以爱国主义为主题的作文。依托当地爱国主义教育资源，各学校每学期要组织学生瞻仰烈士陵园和到爱国主义教育基地、博物馆等地方参观。各中小学要区分不同年级，设计不同内容和难度的题目，举办一次爱国主义知识竞赛。每学期在全校范围内开展一次歌颂祖国、歌颂社会主义、歌颂中国共产党、歌颂民族团结的演讲比赛活动。每学期组织学生自编自讲爱国故事；每学期举办一场校际文艺演出。在学生中开展国家版图教育，开发特色鲜明的爱国主义教育校本课程等。<br />
<br />
据了解，自治区教育工委、教育厅将组织专门人员对各地活动开展情况进行检查，并将活动的开展与自治区德育示范，达标学校评估、验收工作相挂钩。<br />
<br />
延伸阅读：中国将投资逾50亿加强新疆&quot;双语&quot;教育<br />
<br />
国家财政部近日正式批复了《自治区少数民族学前“双语”教育发展保障规划》，计划到2012年，国家和新疆将投入近51亿元人民币，用于“双语”教育发展。未来四年间，将由北京、天津、上海、江苏四省(市)为新疆培训中学“双语”骨干教师2000人。<br />
