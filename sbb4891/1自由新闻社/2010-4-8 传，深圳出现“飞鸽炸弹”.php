													<h2>深圳出现恐怖袭击新动向――“鸽子炸弹”</h2>
						
						
																											<div id="postmessage_554596" class="t_msgfont">深圳出现恐怖袭击新动向――“鸽子炸弹”：一只来历不明并绑有炸弹的鸽子，在飞至福田地标建筑“信息枢纽大厦”西北角附近，突然爆炸，鸽子当场粉身碎骨。爆炸地点距离市政府大楼仅500米左右，当局震动。<br />
<a href="http://bbs1.people.com.cn/postDetail.do?id=98874074" target="_blank">http://bbs1.people.com.cn/postDetail.do?id=98874074</a><br />
<br />
深圳发生怀疑涉及恐袭的「鸽子炸弹」爆炸事件。据本报获悉，上周三（3月31日）上午，一b来历不明并绑有炸药的鸽子飞至福田地标建筑「信息枢纽大厦」西北角附近时突然爆炸，鸽子当场粉身碎骨，但并未造成人命伤亡。由于爆炸地点距离市政府大楼（市民中心）仅500米左右，当局怀疑是「有目的事件」，严密封锁消息，而来自北京的反恐人员已到深圳调查事件。<br />
<br />
　　属于中国电信深圳分部所在地的「信息枢纽大厦」位于福田益田路和福华二路交汇处，记者昨日到现场察看时，大厦保安和治安联防员均对「鸽子炸弹」一事三缄其口。记者在现场并无发现任何爆炸痕?，而与以往不同的是，现场增加了保安数量和驻点巡逻，气氛相对紧张。<br />
<br />
　　据了解：事发于上周三上午，一b身上绑有炸药的鸽子正飞往「信息枢纽大厦」，但在接近目标、穿越树梢时炸弹突然爆炸。因爆炸发生在空中、威力不算大，又值中午时分行人不多，未造成伤亡。有大厦对面商场清洁女工透露，当时她听到一声巨响，只因正在打扫卫生，不远处又有工地，于是认为这声巨响可能是施工声音，并未在意。<br />
<br />
　　信息枢纽大厦由裙楼、主楼及副楼组成。总建筑面积达到18万平方米，主楼48层，高240米；副楼22层，裙楼8层。大厦内有许多国内外知名的电子科技公司，所在区域是福田CBD商务区、高级写字楼密集，不远处又有多个港人聚居的高级楼盘如「星河世纪」等。<br />
<br />
　　记者昨向深圳信鸽协会查询，有工作人员表示未听闻此事，虽然深圳的信鸽都有登记和编号，但这也难证明鸽子主人涉嫌恐袭，因为信鸽经常去别的鸽巢交配或参加比赛时有其他人接手。就算是鸽子炸弹，也应是野鸽，因为信鸽只认识回程路，不认识去程路，除非肇事者在「信息枢纽大厦」内养信鸽，由外面放飞进行攻击。而信鸽负重能力也不大，一般只有50克。<br />
<br />
　　反恐专家、中国现代国际关系学院反恐中心主任李伟在接受本报访问时认为，不排除有人恶作剧或恶意报复，至于鸽子炸弹是否属于恐怖袭击新方式，目前因资料有限尚难下结论。</div>

							
							
							
							
