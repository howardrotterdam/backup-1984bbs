左峤：举起爱国主义大旗<br />
左峤，1982年出生，2005年澳大利亚中国留学生新锐奖提名人。2001年-2005年在澳大利亚学习，2006年回国，现在新华社工作，曾参与大量奥运报道。<br />
<br />
 <br />
<br />
可以说，在广大留澳自费学生中，左峤算是一个异类。他最见不得身在海外的个别中国人不拘小节而自毁形象。<br />
<br />
左峤说，当他看到某些组织或个人身在海外，不仅不宣传中国的文化和发展，却在海外败坏国家形象时，就十分恼火。他很想尽个人的力量，为宣传祖国的历史和文化尽点微薄之力，向不甚了解中国情况的外国友人介绍一个更民主、开放、繁荣的新中国。于是，这个80后的男孩，于2004年成立澳洲中国留学生爱国联盟(The Australian Chinese Students Patriotic Association-ACSPA)。这个爱国联盟是由在堪培拉和悉尼地区的中国留学生创办和维护的非官方、非盈利性质的学生组织，主要利用课余时间、利用网络和一些小型活动，来宣传祖国文化和中国政府和平统一的政策，让澳洲社会较全面地了解一个民主、富强、文明的社会主义现代化中国，并促进中澳两国的文化交流。<br />
<br />
左峤承认，当时很多人对他坚持自发宣传爱国的行为，表示不能理解，更有一些人认为，他的言行颇有点官腔官调，导致对他本人的用意和爱国联盟的性质产生了怀疑。一次偶然的事件，却将左峤这个普通的政治系留学生，推向了网络舆论的刀锋浪尖。<br />
<br />
因为他和爱国联盟的其他成员利用课余时间在网上撰写爱国、反对日本军国主义等主题的文章，2005年被美国CNS新闻网驻新西兰记者站电话采访，并刊登于CNS新闻网上，文章在澳洲某著名政治评论网站上转载，引起了澳洲政界的关注，左峤与一些政见不同的人展开了几个回合的笔枪舌战，由此而引发的舆论影响给他带来了极大的压力，甚至有人称他是间谍。当时面临考试的左峤，被迫关闭了爱国联盟网站，但他不卑不亢的交锋以及理智而严谨的论证，都表现出一个中国留学生的骨气和民族责任感。<br />
<br />
当时，处在风口浪尖的左峤，虽然得到许多人的支持和鼓励，但也有人称他是个典型的、不成熟的“愤青”。对此，左峤虽觉无奈，却也处之泰然。他说：“我父母都是知识分子，所以我从小就受到非常严厉的教育。俗话说‘知恩图报’，祖国培养了我，我为何不在海外为祖国做点事情？”<br />
<br />
不过，令左峤这个爱国学生感到无比欣慰和感动的是，2008年4月，当北京奥运的圣火抵达堪培拉时，全澳各地数万名华人华侨和留学生自发来到堪培拉，以最大的热情和勇气迎接圣火，支持奥运，向澳洲民众和祖国同胞展示在澳华人和留学生对祖国最诚挚最热烈的爱和祝福。这个心直口快的辽宁男孩激动地回忆：“我当时负责新华社奥运报道的网络新闻报道。当我收到那些来自我当年的爱国大本营发来的报道和图片时，我真的很激动，感触非常深刻，留学生真给我们提气！”<br />
<br />
2005年末，左峤回国，到新华社从事国际新闻报道和国际局势的调研工作，并对境外局势和外媒报道进行编译，给相关部门提供更有价值的参考。<br />
<br />
2006年末，左峤在新华网奥运频道负责新华社奥运网络新闻报道，并负责奥组委的采访和新闻发布会直播。他说：“选择新华社工作，正符合我的性格和爱好。我身上既保留了传统的中国儒家思想和过硬的政治理念，又对西方社会的政治体制有着较深的了解，而且能得到领导和同事们的帮助，这份工作让我非常满意和热爱。”<br />
<br />
如今，再次回首当初的留学生涯，左峤感慨地说，留澳那几年，让他对东西方文化的差异和社会制度的差异，有了更透彻的了解。虽然当年差点儿被当成间谍遣送回国，可他至今也不后悔，因为他对得起自己的良心，对得起国家和养育他的父母。他强调说：“在这里，我也借机向那些正在国外留学的学子们说，你们以后学有所成、选择未来发展道路时，希望你们能回国工作，因为国家需要你们，国家也会非常重视你们。”<br />
<br />
这个左峤啊，到哪儿也不忘扛起爱国主义的大旗，即使是接受采访。</div>

							
							
							
							
