													<h2>可能吧封杀升级 1984BBS遭到攻击</h2>
						
						
																											<div id="postmessage_601792" class="t_msgfont">可能吧封杀升级，1984BBS遭到攻击<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;中国新锐博客网站可能吧再次遭到封杀，域名被污染，而另一个信息交流平台1984BBS 则遭到DDOS（分布式阻断服务攻击）攻击。 (博讯 boxun.com)<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;星期三，获得德国之声全球博客大奖的中国新锐网站“可能吧”发现,再次无法访问，此前“可能吧”曾经在今年3月23日谷歌搜索引擎退出中国时，因为一篇写谷歌遭遇审查的文章而遭到关闭。此后 “可能吧”将服务器转移到了海外，又再次遭到长城防火墙的封杀，“可能吧”提供一个特殊的加密通道供访问者订阅。星期三GFW对它的封杀再次升级，将这个秘密通道也完全关闭。“可能吧”在标题为“可能吧”再次被墙，域名遭DNS(域名系y)污染”的公告中表示，“今天，‘可能吧’的新IP再次被墙。同时还发现在某些线路，‘可能吧’遭到DNS污染。”<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;对此，“可能吧”站长阿禅星期四接受本台采访时表示，“他以前封锁‘可能吧’的时候，我是换了IP，然后加了HTTPS，是要抵抗关键字审查，然后又换了一台服务器，可以访问了。这次封杀，以为只要封锁新IP就可以了，但是现在换了 IP也没有用了，除非我不用‘可能吧’这个域名，只用新IP给大家，那么DNS污染不了，不用DNS解析。”<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;阿禅表示，“有人问 ‘可能吧’以后还会不会更新。‘可能吧’有50000多个RSS订阅者，除非他们把Google Reader墙了、把互联网关闭了，否则我找不到不更新的理由。”<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;除了“可能吧”之外，同一日，由新闻媒体人组成的网络交流社区 1984BBS也遭到了大量的DDOS（分布式阻断服务攻击），一度导致无法访问，在哈佛大学报告全球网络审查状况的网站Herdict 上，也出现多次这个网站无法访问的用户报告。1984BBS的取名来自于英国作家奥维尔所写的共产主义的言论控制的小说《1984》，经常邀请网络上的知名人士，例如冉云飞、莫之许等与网友做交流，并举办多场新闻人的交流活动。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;其发起者之一“张书记”告诉本台记者，“现在查到的是一个IP 不断地向我们攻击，但是我们没有确凿的证据说这个IP来自哪儿，不能排除可能是国外的一些黑客捣乱，纯粹是娱乐。我想，把这个证据收集到足够多的时候再拿出来。”<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;DNS 全名是 Domain Name System是负责互联网域名解释的服务器，分布在全球各地，可以告诉网络用户某个网络域名和哪一个服务器相关联，而不会走错门。存在恶意的DNS服务就会把域名和服务器IP地址错误关联，让用户不能达到目标网站。DDOS(Distributed Denial of Services) 就是（分布式阻断服务攻击）则是非常低级的网络攻击手段，主要的原理是用工具向受害网站发出大量的访问单向请求，从而阻塞该网站。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;星期三到星期四，也是全球互联网13台根域名服务器一次重大的安全升级，这次叫做DNSSEC（Domain Name System Security Extensions，域名系统安全扩展）的升级计划，将有效地在全球范围内阻止网络中间人的攻击，此前黑客往往可以劫持一些脆弱的网站作为中间人，改变 DNS请求，并返回用户一个假地址。这种攻击手段在最近中国的网络审查中被发现，人们在不知不觉中被转到另一个网址。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;针对DNS污染问题，网络技术专家克拉苏斯告诉本台记者，“比如以前我访问RFA的网站，我直接看到连接被重置，或者找不到服务器的提示，现在变了，我看到浏览器一直在载入，但是没有结果，始终是一个白色的页面，这给你一种错觉。网民有一个特点，如果你显示一个错误信息，我知道你网站被封掉了，但是总是显示在读取中，可能认为是网络服务因素或者网络变慢。”<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;虽然这次全球网络基础服务升级不是针对中国政府，但是对中国网络用户和类似“可能吧”这样的网站也是一个极大的帮助。 (博讯记者：韩洁) (博讯 boxun.com)</div>

							
							
							
							
