																											<div id="postmessage_533355" class="t_msgfont">环球网实习记者朱盈库报道，在中国西南地区遭受特大旱情之际，东南亚一些国家也出现严重旱情。但是，这些国家认为，中国在湄公河(我国境内为澜沧江)上游修建水坝导致其旱情加重。据路透社25日报道，四个东南亚国家将于下周就此问题向中国提出交涉，但中国官员认为，湄公河干旱与中国大坝无关。<br />
<br />
据路透社报道，湄公河水位遭遇半个世纪来最严重的水位下降，泰国、老挝、越南和柬埔寨的村庄深受其害，其农业、渔业以及饮用水供应都受到影响。作为湄公河委员会(MRC)成员国的这四个东南亚国家将派领导人出席4月3日-5日在泰国海滨城市华欣举行的峰会。中国已同意出席会议。<br />
<br />
泰国法政大学政治学者宋斋说：“下游国家想迫使中国从其大坝放水，但我认为这绝无可能。”他表示，中国自己还面临国内西南地区大旱的压力。天气预报显示，该地区缺水局面短期内不会缓解。<br />
<br />
对于这些东南亚国家将湄公河水位下降归因于中国在上游修建水坝一事，中国驻泰国大使馆政务参赞陈德海23日表示，整个湄公河流域普遍遭遇旱情，中国也是受灾国。湄公河干旱与中国大坝无关。<br />
<br />
陈德海介绍说，澜沧江出境处年均径流量仅占湄公河出海口年均径流量的13.5%，湄公河水量主要来自中国境外湄公河流域。中国与中南半岛几个国家都是好邻居、好伙伴，在今年的澜沧江―湄公河流域旱情中同为受害者，只是所处位置不同，受灾程度不同。<br />
<br />
陈参赞强调，中国在澜沧江水资源开发中一贯高度重视对资源、环境和生态的影响，并与有关国家保持良好沟通。<br />
