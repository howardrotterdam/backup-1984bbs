<br />
各国媒体：中国水坝致旱<br />
<br />
“从一开始，中国大坝的试运转就与湄公河干旱有联系”泰国主流英文报纸《民族报》2日报导称，自从上世纪90年代中国开始在澜沧江流域建立水利大坝以来，下游的泰国和老挝等国家的当地政府和非政府组织就已经监测到，由于鱼类和水生植物资源的减少，当地经济和人民生活受到了不小的影响。因此，澜沧江下游的国家开始怀疑今年的大旱和中国的水利大坝设施有密切的联系。<br />
报导指称：“从一开始，（中国）漫湾大坝的试运转就与1992年到1993年间的湄公河干旱有着不可分割的联系。现在，中国在澜沧江小湾水电站的蓄水更与此次的湄公河干涸有着莫大的联系。<font color="red">小湾水电大坝是世界最高的拱形大坝，其超强的蓄水量是现有的澜沧江上三座水电站总和的5倍，并且需要10年的时间才能将其蓄满。</font>”<br />
泰国《曼谷邮报》2日当天刊发报导称：“湄公河的水量由上游的水利设施所决定，湄公河的水位下降一定与中国境内的水利大坝有着不可分割的联系。”老挝的《万象日报》与《曼谷邮报》的论调如出一辙：“中国水坝造成了湄公河流域的干旱”。<br />
<br />
各国媒体：中国应公布水坝水位数据<br />
<br />
泰国媒体表示，“想证明大坝与大旱无关，请将小湾大坝水位数据公布”。上月25日，中国同意公开关键的河流数据，甚至包括澜沧江景洪和漫湾大坝的核心数据。但《曼谷邮报》2日报导：“与这次大旱有着决定性联系的是小湾大坝的数据。如果中国真的想要与下游国家建立互信关系，想要证明大坝的建设与湄公河大旱没有联系，那么就需要将小湾大坝的蓄水水位数据公诸于众。”<br />
《曼谷邮报》还称，参与此次湄公河峰会的非政府组织成员将会向中国驻泰国曼谷大使馆提起上诉，希望中国能够充分关注其水利大坝对下游国家造成的不良影响。湄公河委员会也将在峰会上与中国展开具有重大意义的双边对话。<br />
<br />
中方驳斥<br />
<br />
水利部官员陈明忠表示，从去年9月至2010年3月，澜沧江的水流量仅达到往年平均水平的4%～18％左右。而中国澜沧江的水量也只占到湄公河总水量的13.5%，“中国绝没有损害下游国家的利益。”而且中国从1996年以来就已经是湄公河委员会的合作国家，从2002年就开始与湄公河委员会分享景洪和漫湾大坝河段在雨季的详细数据。<br />
但一些环保主义者认为，湄公河水位低，部份原因是中国在澜沧江修建的第四座大坝，小湾大坝，所以中国应该分享小湾大坝的详细数据。<br />
<br />
湄公河现状：河没了水 人没了鱼 鱼没了命<br />
<br />
湄公河流域的干旱到底是自然原因还是中国修建水坝造成的，我们不得而知。我们也不知道这样的干旱是否会造成巨型鲶鱼的灭绝。但是干旱的确已经让流域附近的居民们忧心忡忡了。<br />
58岁的Ann Srida一边凝视着水量已不再丰富的湄公河，一边清洗着渔网。她发问说：“如果湄公河里没有了鱼，我靠什么为生？”最近几周，由于湄公河的水位急剧下降，泰国清孔地区的渔民们对此非常忧心。 Ann每天都一大早就出船捕鱼直到傍晚，但是她现在却总是两手空空回家。“现在，我一个月只赚得到700泰铢了。”Ann一辈子都以捕鱼为生。五年前，通过捕鱼，她还获得了不错的收入。但是现在湄公河的水位急剧下降，她说，除了捕鱼，她还不知道自己能做什么以维持生计。“我没有其他选择，不然就只得去城里做劳工。”<br />
48岁的Sak Khamdang说：“我们现在已经停止捕鱼了，除非水位上升，不然，我们能做的事情就只有等待。”Sak Khamdang说，“如果水位再不上升，我就准备到城里谋个工作。”在湄公河对岸的老挝，40岁的Pisai Chankhao在两个月里一条鱼也没有抓到，他只能去城镇里购买他的食物―――鱼。<br />
