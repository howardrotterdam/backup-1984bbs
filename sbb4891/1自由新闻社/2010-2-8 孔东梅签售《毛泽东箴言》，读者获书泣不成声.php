													<h2>孔东梅签售《毛泽东箴言》，读者获书泣不成声</h2>
						
						
																											<div id="postmessage_472627" class="t_msgfont"><a href="http://news.sina.com.cn/s/2010-02-08/151219653403.shtml" target="_blank">http://news.sina.com.cn/s/2010-02-08/151219653403.shtml</a><br />
<a href="http://www.sina.com.cn" target="_blank">http://www.sina.com.cn</a>&nbsp;&nbsp;2010年02月08日15:12&nbsp;&nbsp;大众网-生活日报<br />
<br />
<img src="http://i0.sinaimg.cn/dy/s/2010-02-08/U4126P1T1D19653403F21DT20100208151343.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
孔东梅（右）在签售《毛泽东箴言》。 记者 王鸿光 摄<br />
<img src="http://i2.sinaimg.cn/dy/s/2010-02-08/U4126P1T1D19653403F23DT20100208151343.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
拿到签名版《毛泽东箴言》后，一位读者激动地哭了。 记者 王鸿光 摄<br />
<br />
　　昨日，毛泽东的外孙女孔东梅来济为其出品的《毛泽东箴言》举办签售活动，在签名售书前 ，孔东梅接受了本报记者的采访。身材高挑清瘦、下巴有颗明显的毛泽东痣的孔东梅话语轻柔、穿着时尚。据孔东梅介绍，《毛泽东箴言》共收录箴言360条，分为观世、正己、待人、处事4篇，书中所收箴言摘选自建国以来正式出版的《毛泽东选集》、《毛泽东文集》、《建国以来毛泽东文稿》等主要著作。<br />
<br />
　　出版《毛泽东箴言》把经典著作变成“大众读物”<br />
<br />
　　攻读过新闻传播学的孔东梅提出过红色文化时尚化的概念，此次的《毛泽东箴言》正是这一概念的具体实践。孔东梅说：“我希望能为广大读者特别是青少年读者提供简洁、直观的阅读载体，这是一种在新世纪把毛泽东思想通俗化、普及化的积极尝试。我在美国留学时，看到西方年轻人接受爱国主义教育的程度很高。我当时就开始反思，我们的红色经典文化应该以什么形式出现，才能让年轻人从内心深处真正接受和喜欢？我觉得应让红色经典加上时尚元素吸引‘80后’、 ‘90后’进行阅读，要突破传统的传播模式，用他们喜欢的方式来进行，所以我希望红色经典能时尚化，加入一些现代、时尚的元素，这样会有更多年轻人关注，也就更利于红色经典文化的传承与传播。”孔东梅认为，文化是一个很好的交流平台，它可以突破政治的界限，在文化的平台上大家可以畅所欲言。<br />
<br />
　　孔东梅告诉记者，出版这部书的想法已经酝酿了5年的时间，之所以花费这么长的时间，就是因为一直在思考怎么样才能使毛泽东思想读起来不那么费力，“我们希望做一本简单、让大家可以轻松阅读的书。在选择条目时，我们拿掉了适合当时历史背景但不适合当今的话，留下的都是一些最朴实的箴言，适合男女老少阅读。”孔东梅表示，毛泽东在漫长的战争和建设年代中，锤炼出深入浅出的个性化语言，其思想丰富多样，博大精深，从中做一些取舍并不是一件容易的事， “把经典著作变成适合普通人阅读的书，是传播方式的变革。”<br />
<br />
　　作为毛泽东的外孙女并不喜欢“脸谱化的毛泽东”<br />
<br />
　　谈及对外公的印象，孔东梅话语中除透着崇敬之外，还含有一点感慨和遗憾：“我是1972年出生的，母亲是毛泽东长女李敏，外婆是贺子珍。我自小与外婆贺子珍一起在上海生活，一直没有和外公见过面，但他对我的影响是巨大的。我母亲和外婆经常谈到外公，而且以外公的教导作为处世做人的标准。而在这种熏陶中，外公给我的最大影响，就是一种执著、百折不挠的精神，他虽然离开了我们，但我们不能遗忘这段历史。”<br />
<br />
　　“毛泽东的外孙女”这一特殊身份对孔东梅产生了什么影响呢？“作为毛泽东的后人，我深深地感到自豪，影响肯定会有一些，与别人有些地方也会不一样。但我认为人首先是做好自己。”孔东梅表示。同时孔东梅向记者透露，因为自己从小在上海长大，所以在生活习惯上没有受到毛泽东太大的影响，她并不能吃辣，至于红烧肉也只是吃上一点点，因为她“怕胖”。对于近年来出现的多部有关毛泽东的影视作品，孔东梅直言，“我不是太赞成那种‘脸谱化的毛泽东’。”有创新的、有突破性的“毛泽东”都比较吸引她，比如前几年拍摄的《长征》，从一个相对人性化的角度来刻画，就很吸引人。至于自己做的毛泽东系列的作品，今后还会再出新。<br />
<br />
　　记者问起她现在从事的工作情况，孔东梅介绍，她创办的文化公司名为“东润菊香书屋”，是因为外公在中南海丰泽园菊香书屋居住多年，“取这个名字，使我能时时想起外公对我们后代的润泽之情。我亲自出任公司董事长，为的就是尽自己的能力收集、整理、研究和传播‘红色经典文化’，以展览、出版和影视为载体，将其发扬光大，这是我的责任，而我自己也乐在其中。”<br />
<br />
　　《毛泽东箴言》签售现场火爆<br />
<br />
　　昨天下午三点半，毛泽东的外孙女孔东梅在泉城路新华书店为其出品的作品《毛泽东箴言》举办签名售书活动，该书是近年来编订以及出版规格最高的毛泽东著作。签售现场异常火爆，下午刚过两点，签售现场就已经有不少读者在排队了，售书开始时队伍已经排到了街上，其中不仅有白发苍苍的老者，还有很多年轻人和学生，不少读者都是买了四五本书。人们都激动地争相和孔东梅女士合影，还有人兴奋地流下了眼泪。<br />
<br />
　　“每次看都会掉眼泪”<br />
<br />
　　在排成一条长龙的队伍中，记者发现了一位白发苍苍的老者，才两点多她就来排队了，一直等了一个多小时，因为太累她不得不坐在工作人员准备的椅子上休息。“我对毛泽东主席的感情太深厚了，像我们这个年纪都是从毛泽东主席的年代过来的，他的事迹太感人了，每次看我都会忍不住掉眼泪。”这次她一下子买了四本《毛泽东箴言》，“等我的小外孙长大了给他看，毛泽东主席说的话可以称得上是心理学著作了，里面的每句话都意味深长，蕴含的辩证法原理对现代生活也是大有用处。”<br />
<br />
　　“毛主席的话是永恒的经典”<br />
<br />
　　拿到签名的《毛泽东箴言》后，高中生小李独自站在人群外面泣不成声，她给自己还有爷爷奶奶、外公外婆都买了一本，她因为激动而忍不住哭了，“毛主席的话是永恒的经典，激励着我们不断上进，教给我们做人的道理，在现在这个时代，虽然和以前有着天翻地覆的变化，人们的思想也和以前大不一样了，甚至于出现了对社会、对国家不利的思想，所以毛泽东思想在现在尤为重要。”面对记者的采访，小李仍然难以控制自己的情绪，眼泪像断了线的珠子一样止不住，“最遗憾的事情是没能和孔东梅女士合影，我会牢记里面的每一句话的。”<br />
<br />
