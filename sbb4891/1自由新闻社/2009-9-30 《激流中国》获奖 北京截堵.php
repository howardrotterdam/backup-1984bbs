实行独生子女政策至今的中国社会，正以超过先进国家的速度迅速老龄化。 <br />
现在的老龄化率（中国规定60岁以上者为老年人）虽为11%，但预计到2050年，每四个人中就有一个超过六十岁。如今经济成长依然在继续，但由于社会保障尚未完备，如何赡养急剧膨胀的老年人口，正成为严峻的课题。 <br />
<br />
在此情况下，养老院作为新的解决途径而受到注目。但要入住设备齐全的设施，必须花费高额的费用，在向来重视“养儿防老”传统的中国，不愿进养老院的老年人，和不愿把老人送入的家属也为数众多。 <br />
<br />
我们的摄影机，第一次进入了老龄化率高达18%、仅次于上海的青岛市的养老院。目前生活在那里的老年人有500人，房位处于供不应求的状态。然而入居者各自有不同的现实问题，与日本的情况大相径庭。<br />
<br />
本集通过生活在中国老人院的老年人的身姿，描写正在急剧高龄化的中国社会的现状。<br />
