													<h2>《激流中国》获奖 北京截堵</h2>
						
						
																											<div id="postmessage_264913" class="t_msgfont">据日本共同社9月27日消息，在意大利都灵举行的电视节目竞赛“第61届意大利奖”上，NHK电视台特辑《激流中国》之“病人的长龙――13亿人的医疗”荣获最佳记录片奖。<br />
<br />
该片曾在今年1月的第22届法国国际电视节(FIPA)荣获记录片部门金奖，6月在第49届蒙特卡洛电视节上荣获最佳记录片奖。<br />
<br />
FIPA是欧洲最权威的影视节之一，第22届法国国际电视节(FIPA)在法国西部城镇比亚里茨举行，这是日本的记录片首次在FIPA上获奖。<br />
<br />
蒙特卡洛电视节由摩纳哥公国主办，评审对该片给予肯定：“通过一个极其精彩的故事展现了被拜金主义与令人绝望的差距渗透的现实社会”。<br />
<br />
《激流中国》：全面探讨中国社会<br />
<br />
《激流中国》是日本放送协会制作的电视记录片《NHK特集》的系列题目之一，原规划24集，要以一年多的时间至2008年北京奥运前播放完毕；相关采访皆经过中国官方批准。从2007年4月开播。<br />
<br />
NHK对其的简介是：“北京奥运会即将来临，中国正试图转向成为21世纪的大国，而同时在国内又面对诸多矛盾，面临困难的抉择。这个专辑深入采访中国变化的现场，描绘其苦恼及其纠葛。”《激流中国》每集开头以此段文字作为指引：“夙愿的北京奥林匹克还有一年，试图从经济优先转向为真正大国的中国，如今种种矛盾，汇集成激流，汹涌而至。”<br />
<br />
中共态度 全面封杀<br />
<br />
该片的放映皆获得中日观众的好评，尤其在中国收视率极高、备受瞩目。不过，前两集播出后，北京当局认为有损中国形象，透过外交管道向日方抗议，导致该片一度停播，直到8月恢复播放。<br />
<br />
但有趣的是，《激流中国》采访组已经过中共相关部门严密审阅及得到批准，各集内容也做了详细的审查描述与定论，认为前两集有关中国的贫富差距与新闻严管的内容“基调消极，观点负面，造成了不良影响”，又称《激流中国》有“诬称中国的新闻自由”。但后两集的老年化与水资源的内容则“较积极客观”。<br />
<br />
开播当初，该片在中国网络上并无引发任何议论，但却被中国雅虎搜索列入“敏感关键字”，使用中国雅虎无法查询到《激流中国》的任何相关结果。之后由于内容揭示中国社会现状，很快就造成轰动，网友看后发表大量感言，引发中共当局的强烈关注；随后《激流中国》在中国大陆任何一个搜索引擎中被“和谐”查不到任何讯息。在eD2k分享网站VeryCD.com上的下载链接也被彻底删除。中共外交部新闻司与驻日使馆曾向NHK提出交涉，认为其“不客观、误导观众、损害中国形象”。但讽刺的是，今次已经是该片第三次在世界获奖。<br />
<br />
《激流中国》内容简介（在中国分为24集）<br />
<br />
序章：富人与农民工<br />
第1集：某杂志编辑部 60天的攻防<br />
第2集：青岛 老人院的故事<br />
第3集：确保北京的水利――迫近水危机<br />
第4集：民诉官――围绕土地问题的攻防<br />
第5集：西藏 从圣地寻求财富<br />
第6集：贴身采访 共产党 地方干部<br />
第7集：有超凡魅力的总经理吃官司――跟踪采访&#12539;品牌骚动<br />
第8集： 5年级1班 小皇帝之泪<br />
第9集：老师从上海来了――在贫穷的村落<br />
第10集：北京 愤怒的新社区――居民势力沸腾起来<br />
第11集：病人的长龙――13亿人的医疗<br />
最终集：告发！ 揭发！――与环境破坏作斗争<br />
<br />
荣获最佳记录片奖的《病人的长龙――13亿人的医疗》简介：<br />
<br />
中国现有的医疗体系。在没有国家大力支持的情况下，医院仍然会把病人看作“唐僧肉”，是其利润的主要来源。从而造成一种恶性循环性的国家体系坍塌。从中只是滋生了“吸血鬼式”的得益者。处方药物降价、扶持中医事业发展、严打医生和医院收受回扣……中国政府的一系列治标之举，真的能否使国民享受到实质的好处，让人质疑。还是政府转移焦点，虚增国民对执政者的信心。企图使人民忘记当政者责任。倘若中国政府始终拿不出决心和办法来整顿医疗体系的弊端，本质性无法彻底转变的中国医疗体系，而只知年复一年重复强调的“改革”，那最终只是一句政治掌权者的老调重弹与漫天空谈。从而造成治丝益棼的恶性循环，愈陷愈深。世界人口最多的国家的老百姓如何使自身过得富足，中国共产党的一党执政能否一直巩固其执政地位。在步入经济全球化的二十一世纪，改革加剧的中国。各自的挑战和考验才刚刚开始……<br />
