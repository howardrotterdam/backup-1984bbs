													<h2>这样的生活是为了什么</h2>
						
						
																											<div id="postmessage_520657" class="t_msgfont">梦里的生活好像都是自动发生的，而不是我有意识、又主动改变的。白天生活在想象世界中也如此。我一直都很难感觉到并接受世界中的事实，甚至有人说，我所感觉到的对我不起作用。想象中的生活很舒服，在想象中我们可以不管真理，都根据自己想象出来的一种生活模式去思考。真理不舒服，因为在追求真理时，我们需要经常想得到自己的思考和行为是否有根据。其实，在我们的现代社会中，大部分人都生活在某种想象模式中，他们根据自己对于世界的某种想象去对待世界。可是同时，我一直都很想使用我的感觉器官去接受事实。我觉得，在追求真理的过程中，感觉器官能感到的还不够--它们给我带来的认识还不够。感觉不应该仅仅是用来享受的。<br />
另外，我以前一直寻找着一个理想的人并以为在某一个村子能找到他。我是想从他得到力量。可是后来我知道，这个人是不存在的，所以我只好把他写成书，给他一种想象中的存在。不过最后我还是发现：只有我自己去成为这个理想的人，他真实的存在才是有可能的。<br />
所以，前几年，我在广西一个县城的初中义务当英语老师。不过，因为我的课程不是按照标准来开展的，家长有意见，过了一个学期我就被开除了。所以，2000 年2月，我就转到了属于兴坪镇的一个在村里的大坪子八一初中当老师。<br />
当时，我还没有认识到我自己对于学生的好感（同感、共同感、吸引力、溶在一起）和反感（距离感、维持自己的特点、排斥感），也没有认识到学生的好感和反感，而正是这个缺点造成了很多纪律问题。当时，我羡慕自己学生身上所展现的小孩的特点，希望为了我自己而去占有学生的这些特点。学生感觉到了我怕从他们那里得不到这些特点，就利用了我这种不好的愿望来控制我，使我搞不好纪律问题。不过，我当时正在学的《适合人类的教育》帮助我脱离了这些问题。在后边的几年内，我慢慢地学会了去观察我自己的好感和反感如何影响到学生的做法。我慢慢地学会了从这些观察来调整我的行为和课程。通过观察，我就慢慢地把好感转变成了行动和意志，把反感转变成了认识。而我自己的转变又引起了学生不同的态度。<br />
2001年2月，我转到了东兰县隘洞镇初中任地理、美术和英语老师。我发挥的不重视成绩的教育又害了学生的升学率。如果不想让他们被淘汰而离开学校，我只能自己离开。后来，因为我在他们学校做过的那种影响到成绩的实验，领导再也不让第二个老师做实验，使得后边的老师都很难。所以，后来我就到了我一个学生所在的村子去跟没有上学机会的孩子做教育活动。我就在这些地方做教育实验，尝试在教育改革方面起到帮助作用。<br />
在屯里做实验，因为不想满足教育局的要求，不想把所有本来灵活的做法适应死板的标准、不想把学生的头脑格式化，我就说：&quot;我不想依赖任何单位，不可能办学，不可能发毕业证书。如果你们有机会上普通学校，不如上那个学校。&quot;<br />
当时，家长和其他老师听到后就问：&quot;如果你没有对于教育固定的目标、没有一个要达到的衡量标准，你怎么能管理好学生的学习情况？如果你不能定下教育出来的人能满足一些怎么样的标准，谁愿意送他的小孩？人家送小孩上学只是因为他们有目标，得到一个怎么样的小孩的那种目标。&quot;<br />
我确实不能回答他们的问题，所以只好说：&quot;我的学生要找到自己生活的路，和生活带来的、自己的、对社会有贡献的任务。我想给学生走这条路和完成任务所需要的才能和力量。可是什么是他们的路和任务，我不可能知道。&quot;<br />
老师们继续说：&quot;你怎么做得到？&quot;我回答道：&quot;我知道，我不能达到你们想像的那种成果，肯定不能。可是在做实验的过程中我会碰到很多问题，从问题得到理解。然后别人从这些就可以学到很多。为了这一点，我要尝试做不可能成功的事。&quot;<br />
我身边的人总是会问我：&quot;你干嘛不接受工资？&quot;我向谁能要工资呢？我只在做我自己感兴趣的事，谁为了让人做自己感兴趣的会发工资？拿工资的人是不自由的。他们还会问：&quot;这里的生活很艰苦，你习惯吗？&quot;他们都认为我在这里很难受。实际上不是这样的，而是自由的。也许他们的意思是，没有人要求我要做什么的生活是苦的，然后他们就要求我到城市？反正我也不理解他们的意思。可能他们忘了每一个人不同的兴趣。如果一个人的兴趣是奢侈的生活，他在村里当然难受。如果一个人的兴趣就是村里安静的生活，他在村里怎么会感觉到难受？只要时间够长，什么都会变成习惯、变成容易接受的事。生活条件和做事，都是这样的。<br />
我的兴趣和他们的不一样，我对消费不感兴趣。如果我不能追求我感兴趣的事情，我也会缺少一些感受，会因为得不到这种感受而饥渴。对消费生活的追求只会让我萎缩，甚至会让我失去对于生活的信心。<br />
有的学生问我：&quot;什么是最幸福的生活？&quot;我说：&quot;能做自己感兴趣的事是最幸福的。比如，我想当研究家。&quot;他们又问：&quot;可是当研究家需要条件、需要认可，不是随便一个人就能当的。&quot;我说：&quot;如果还要追求认可，我就要活得太累。我虽然没有得到认可、任务、工资和研究的资料，可是我可以自己发现任务，在没有可研究的材料时，我可以自己去做实验，然后来研究我自己的做法。这样不是能满足我的兴趣吗？我不能要求别人做我想研究的事。&quot;<br />
这当然也是说，我只能去别人不愿意去的地方，因为在那里我才不站别人的地。我身边的人大部分都认为，有了像我这样的生活，生活就失去了它的意义和价值。不过，为什么要对自己有价值呢？对世界的发展有价值就已经足够了吧。听说县政府里有人说我这个人很傻，不懂得去好的地方。根据他们物质主义的价值观，他们无法理解除了消费之外还有其它可追求的东西。<br />
如果来观察世界上的发展，比如科学的发展，包括原子弹等等，再想像我们仍然只去思考自己范围的事、继续只思考对于自己党派和家人的利益，对整个人类来说不是很危险吗？有了像原子弹那种技术，人们就必须为了全人类而思考。如果只有科学的发展，可是人的思想和做法并没有发展，我们就会把我们自己互相消灭掉了。所以可以说，&quot;不是为了钱，而只因为有兴趣才做事&quot;，不是一件难得的事，而是一件必须的事。<br />
很多人过得很可怜，天天做自己不愿意做的事情。然后他们就用钱买的东西来安慰自己，让自己忘掉不能做理想的事。我的情况不一样，我直接去做我愿意做的事，所以我也不需要拿物质的享受来安慰自己。有人说，他们也想做自己愿意做的，也想做自由人，不过追求这些而不追求（死后都要放弃的）钱在社会的眼光中是不可理解的。在我想不管别人的理解时，他们说&quot;不行，你没有得到承认，也没有生活保障&quot;。不过我认为：生命长远，可是为了得到社会的承认永远要做不愿意做的事，不如生命短暂，可是做了自己愿意做的事。<br />
只有自己自由能认识和负责的真理，才会给我带来做事所需要的力量。<br />
有的人说：&quot;世界上所有的进步和推动人的力量都基于自己得到利益自私的追求。如果没有自私的心，就没有人做事。他们就是想要这种只为了物质消费的生活，根本不想改变。我为什么还要说服他们？既然城市人要社会现在的样子，我就不应该&quot;帮&quot;他们改变。<br />
农民又说，他们对不起我。但如果不是他们让我住在他们的家、如果不是让我吃他们的饭，我也无法解决我自己的生活。只因为他们来解决，我才能追求自己感兴趣的事。<br />
每次到大城市的&quot;现代社会&quot;时，我都觉得好像自己生活在假造的、一种电视里才存在的广告世界里。这个社会的人具有的物质超过了我的想像能力，好像他们所具有的东西在世界上是多余的样子。他们的生活方式让在乡村生活的我感到非常不现实，离真实的生活太远。对我来说，现实的生活是那种炎热又没有水喝、很累又没有地方睡觉、付出劳动力又没有回报、想听音乐可是除了学校的广播之外都没有机会听、想写和发资料但很少有电脑、电或电话线的生活。而有电、能上网又有饮料喝的生活往往只是在梦想中存在。如果我得到，这些也会很快被别人用完或用坏了。在我熟悉的生活环境，一个学生一星期的伙食费是2元钱。<br />
在城市做教育实验也不现实，因为在城市要满足很多的物质条件、要花很多的钱，这样，各种各样的部门才能批准教育工作。如果要生活在城市，我就必须花大部分的力量和时间去解决我个人的生活，必须挣钱，必须为了钱做事，不可能追求自己的兴趣。如果要挣这个钱，就需要出卖我的理想，卖给做宣传工作的人，可是这样好吗？因为我不想这样做，我只能在政府部门不重视也不太管的地方来做。城市已经有的老师那么多，那么好。我又不会跟他们竞争，所以就觉得自己属于那些被淘汰但自由的人。另外，城市的学校，因为家长很怕，因为担心学生的成绩，也不允许做真正的实验，只允许根据固定（保险）的模式去做，没有老师自己想的机会。城市的小孩能否接受不标准的课程也是一个问题。城市的小孩什么都太多，好象是一种很难进得去的大快肥肉、他们也好象已经满并关闭的样子。农村小孩的样子就好象什么都很容易透过和&quot;塑造&quot;他们并变成他们的生活。城市小孩因为已经满了，什么都会被他们堵塞。<br />
最后一个在城市不现实的因素是，城市的生活节奏太紧张，根本做不了研究工作。<br />
我已经得了一种紧张不安的病。再去城市追求那么多东西的话，我因为痛就快要发疯了。在神经痛的时候我会觉得，讲究&quot;舒服&quot;是完全多余的。只要我的神经不痛，生活就非常地美好和舒服。<br />
但在&quot;现代社会&quot;中的人好像最喜欢那种尽可能刺激、好玩和不清醒的生活。清醒的生活反而让他们难受，在清醒状态下，他们不喜欢做事。关手机对他们来说已经算是一种练功的状态。连农民也在追求刺激，他们最怕的是安静和闷。我觉得，这样的生活也很不现实，我不仅适应不了城市竞争刺激的生活，也不愿意在这种幻想中去生活，我想要的是真实。<br />
不真实的生活给我们的社会带来了很多问题。比如说学校里的为了成绩的&quot;淘汰赛&quot;：这种&quot;淘汰赛&quot;不是适合每一个学生的事情。部分学生觉得，自己适应不了学校里的生活，那么多的课使得自己的梦想都已经没有了，真像是监狱里的样子。有的学生就很难参与。参与的话，他们会感到很空虚，缺少能体验和感受的机会。他们会寻找一些其它东西，而在香港录像的引导下，他们找到的、能给他们带来感受的是党派的世界。如果我们不给那些不适应成绩&quot;淘汰赛&quot;的学生一些更好的内容，我们就无法解决学校里的暴力问题。<br />
在大城市，也有人想说服我和他们做&quot;大事&quot;。他们让我进入那种梦想中（可是不安）的生活。我觉得，他们让我进入的完全是假（幻想）的、像电视上演出来的一样。他们想让我在大城市开展一些为了帮助贫困农村的活动，可是这怎么可能呢？为了农村离开农村？在大城市做一些精美的关于穷人的介绍资料，让城市的人梦想农村的&quot;事实&quot;？这也太不现实。不如自己留在农村让农民参与到我的生活。这样就能发现，以前自己在城市的时候很容易看见能做些什么事情，可是一到了农村就看不见了，原来见过的也变得很假。</div>

							
							
							
							
