													<h2>工信部部长李毅中：警惕美国IBM“智慧地球”陷阱</h2>
						
						
																											<div id="postmessage_573802" class="t_msgfont">来源: IT商业新闻网 　日期：2010.04.15 09:02<br />
<br />
【IT商业新闻网讯】（记者 方圆）近日，工业和信息化部部长李毅中表示，通过传感网和互联网的应用，“智慧地球”可以极大提高效率，产生更大的效益，但美国试图用其信息网络技术，控制各国的经济，我国发展战略性新兴产业时，必须提高警惕，不能受制于人。<br />
<br />
李毅中部长是在2010年经贸形势报告会上提出上述观点的。他表示，要大力发展3G、物联网等新兴产业。对于外国这些新的理念和新的战略，要有所启迪，也要有所警惕。<br />
<br />
李毅中提醒不能受制于人<br />
<br />
物联网是全球公认的继计算机、互联网与移动通信网之后的世界信息产业又一次新的信息化浪潮。在物联网上，所有物品，像闹钟以及电冰箱等都可以连接到互联网上，实现智能化。物联网已经成为我国重点战略，国务院总理温家宝更是将物联网列为5大必争产业制高点之一。<br />
<br />
有评论认为，物联网完全是新概念，国内外对物联网的内涵和产业发展仍处在研究探讨阶段，还没有形成共识。对于战略性新兴产业做超前研究是必要的，但作为实际工作安排一定要十分慎重。而且，温家宝总理在政府工作报告中对战略性新兴产业的提法也只是 “大力培育”，并没有鼓励对尚未成熟的新兴产业进行大规模产业推广。不过，蓝色巨人IBM似乎对此显得极为急躁，大力推广其“智慧地球”。<br />
<br />
IBM认为，该公司提出到“智慧地球”，包含了物联网概念。包括IBM大中华区董事长兼首席执行总裁钱大群在内的多名IBM高管在不同场合公开表示，“智慧地球”要做的事情，就是“三个“i”，即是物联化、互联化和智能化，这三个化是整个“智慧地球”所代表的理念。IBM中国研究院副院长陈滢认为，“物联化”就是包括了物联网的概念在里面，概括出地球中有生命迹象的物体都能被感测到，无论通过什么样的方法，科学能够到达的地方想感测物体都可以感测。<br />
<br />
IBM还举例说明，“智慧地球”已经完全成熟，可以推广应用，该公司完全有能力提供“智慧地球”的相关信息技术和解决方案。<br />
<br />
李毅中则强调说，“智慧地球”就是通过在基础设施和制造业上大量设立传感器，捕捉运行过程中的各种信息，然后通过传感网，进入互联网，通过计算机分析处理发出智慧指令，再反馈回去，到传感器，到基础设施和制造业上，极大提高效率，产生更大的效益。美国试图用它的信息网络技术，小到控制一台计算机、一台发电机，大到控制一个行业，控制各国的经济。所以，对于外国这些新的理念和新的战略，我们既要有启迪，大力发展战略性新兴产业，也要提高警惕，不能受制于人。<br />
<br />
对国外电子科技技术的依赖，对中国经济影响极为严重。此前有报道称，苹果iPhone拥有高达200%的毛利，但代工企业富士康的毛利仅为2%，富士康相关人员以“悲哀”形容这一现象。仅仅一款产品尚且如此，如果整个产业都是这样，后果可想而知。<br />
<br />
IBM“智慧地球”威胁国家信息安全<br />
<br />
比个人利益更为重要的是国家利益，比经济得失更要害的是国家信息安全。李毅中从经济角度谈到了美国对中国的控制，但有业内人士认为，中国更需要警惕“智慧地球”对我国信息安全乃至国家安全的危害。<br />
<br />
2009年2月开始，IBM在中国以极其猛烈的方式推广“智慧地球”概念，包括建立中文网站，在门户网站，电视媒体，公交站台等进行广告轰炸，举办各种研讨会进行宣传，这种宣传力度在其他国家是罕见的。IBM商务人员积极的在中国企业和政府部门中推广“智慧地球”应用，并开始达成诸多协议。<br />
<br />
在IBM的宣传中，“智慧地球”所包括的领域极为广泛，有电网、铁路、桥梁、隧道、公路、建筑、供水系统、大坝、油气管道等等。这些领域涉及到了民生基建和国家战略，甚至是军事领域的信息。专家认为，如果这些信息被国外IT巨头获取或被他国操纵，造成的后果是不可想象的。<br />
<br />
“IBM公司是与美国国家战略挂钩的公司，我国如与其合作的话，上述领域和金融领域的动态信息将很容易被IBM等IT巨头获取或操纵，进而被美国政府所利用。还有一种风险是通过IBM公司的软件系统，军事人员也完全可以反向操控上述设备。”中国科学院虚拟经济与数据科学研究中心客座研究员刘锋无比担忧地说。<br />
<br />
此前，还曾有业内人士提出，中国安全部门应该关注以下3个疑问：1、IBM“智慧地球”在中国实施的时候，涉及中国国家安全的信息如何得到保证？2、中国大型行业企业，中国地方政府与IBM“智慧地球”项目合作时，如何保证国家基础设施关键信息不会外泄？3、IBM为什么在中国大力推广“智慧地球”，在世界其他国家为什么没有这种力度？<br />
<br />
我国物联网研究与国际同步<br />
<br />
不可否认，IBM超前的技术和出色的产品对信息产业的发展有非凡的引领意义，但是否信息产业每一个链条都要依赖于IBM呢？中国“物联网”战略要成功，是否必须跟随IBM所谓“智慧地球”的脚步呢？世界其余国家也都在加大物联网研发力度，中国的研究在国际上处于怎样的地位？<br />
<br />
对此，李毅中认为，我国物联网研究与国际同步。他表示，当前各地对于物联网都非常重视，工信部和江苏省无锡市正在组建国家级物联网研发中心。 “(我们国家的)物联网有一定的基础，在国际水平线上，我们并不落后。”李毅中说。<br />
<br />
中国科学院上海微系统与信息技术研究所副所长、国家物联网技术产业联盟筹备组组长刘海涛日前也曾经表示，中国物联网技术发展与国际同步，并已在诸多行业得到初步应用，而利用物联网技术，上海世博会园区的防入侵系统正在工程实施中。</div>

							
							
							
							
