													<h2>贾庆林：我们既不是一党制也不是多党制</h2>
						
						
																											<div id="postmessage_308970" class="t_msgfont">贾庆林:中国政党制度同一党及多党制有四大不同<br />
2009年11月01日 中国新闻网<br />
中新网北京11月1日电 中共党报《人民日报》1日刊登中共中央政治局常委、全国政协主席贾庆林的文章《在中国特色社会主义道路上不断完善和发展中国共产党领导的多党合作和政治协商制度》，分析指出了中国政党制度的巨大优势，以及同国外一党制和多党制的根本区别。<br />
<br />
文章强调，中国共产党领导的多党合作和政治协商制度，植根于中华民族生存和发展的深厚土壤，产生于中国共产党同各民主党派和无党派人士团结奋斗的风雨征程，发展于建设中国特色社会主义的伟大实践，具有鲜明的中国特色和中国气派。<br />
<br />
文章从四个方面进行了具体阐述:<br />
<br />
――政党关系。西方两党制和多党制以不同政治利益集团通过选举和竞争掌握权力为基本特点。这决定了其政党之间必然要为争取选民、上台执政而相互争权夺利、彼此排斥倾轧。实行一党制的国家大多不允许其他政党存在和发展。<br />
<br />
中国共产党领导的多党合作和政治协商制度，既不同于西方两党制和多党制那种你上我下的权力争夺型的政党关系，也不同于一党制那种权力垄断型的政党关系，而是一种民主协商、肝胆相照的崭新的合作型政党关系。各民主党派和无党派人士自觉接受中国共产党领导，与中国共产党亲密合作，共同致力于中国的革命、建设和改革事业。正如江泽民所指出的，“这既避免了多党竞争、相互倾轧造成的政治动荡，又避免了一党专制、缺少监督导致的种种弊端。我国政党制度的巨大优势就在这里，同国外一党制和多党制的根本区别也在这里。”<br />
<br />
――政权运作方式。在实行西方两党制和多党制的国家里，几百年来形成的政权运作方式是各政党轮流执政，只有执政党才能主导国家行政权力，在野党或反对党一般不能直接参与政府对国家事务的管理。在实行一党制的国家里，由于一党独揽国家权力，缺少必要的监督，往往容易导致政治专制和体制僵化。<br />
<br />
中国是一个社会主义国家，中国共产党作为执政党，依法长期执政，这是宪法确定的。中国共产党真诚同党外人士合作共事，充分听取各民主党派和无党派人士的意见和建议，自觉接受他们的民主监督，努力实现科学执政、民主执政、依法执政。各民主党派作为致力于中国特色社会主义事业的参政党，参加国家政权，参与国家大政方针和国家领导人选的协商，参与国家事务的管理，参与国家方针政策、法律法规的制定和执行，在国家社会政治生活中发挥着重要作用。<br />
<br />
――协调利益关系。实行西方两党制和多党制的国家，由于各党派不可调和的利益冲突，无论哪个政党上台执政，都不可能代表全体人民的意愿和利益。<br />
<br />
中国共产党始终代表最广大人民的根本利益，民主党派和无党派人士反映和代表各自所联系群众的具体利益和要求，都把建设富强民主文明和谐的社会主义现代化国家作为最大政治共识。同时，在同民主党派和无党派人士合作中，中国共产党始终坚持照顾同盟者的具体利益，通过制定方针政策、法律法规和相应措施，保障他们及所联系群众的权益，并通过民主协商最广泛地听取社会各方面的意见愿望，最大限度地满足社会各方面的利益诉求。<br />
<br />
――民主形式。在西方两党制和多党制下，各政党为了赢得更多的选票，一般也会适当考虑普通民众的意愿，但归根到底是受垄断资本利益集团左右的。<br />
<br />
中国共产党领导的多党合作和政治协商制度以合作、参与、协商为基本精神，以团结、民主、和谐为本质属性，是中国共产党和中国人民在民主形式方面的伟大创造。中国共产党同各民主党派和无党派人士就事关国计民生的重大问题进行协商，在人民政协同各民主党派和各界代表人士广泛协商，能够在中国特色社会主义共同目标下把中国共产党领导和多党派合作有机结合起来，实现广泛参与和集中领导的统一、社会进步和国家稳定的统一、充满活力和富有效率的统一，体现了中国社会主义政治制度和政党制度的特点和优势。 <br />
<br />
文章指出，当今世界正处在大发展大变革大调整时期，中国正处在进一步发展的重要战略机遇期，在新的历史起点上向前迈进，多党合作和人民政协事业面临着新的发展机遇。要坚持长期共存、互相监督、肝胆相照、荣辱与共的方针，总结运用和丰富发展多党合作和政治协商的宝贵经验，不断推进理论创新、制度创新和工作创新，进一步把中国共产党领导的多党合作和政治协商制度坚持好、完善好，把这一制度的优势和作用运用好、发挥好。</div>

							
							
							
							
