（香港）&nbsp; &nbsp;(2009-07-11) <br />
<br />
早报导读 <br />
&nbsp;&nbsp;<br />
　　（联合早报网讯）香港文汇报报道，别人防毒他“养毒”，别人怕毒他爱毒。成都军区某团司令部自动化站三级士官钱兴伟，因擅“玩毒”被称为“毒王”。在部队自动化站机房一个被锁进保险柜的机密硬盘中，存储了2200余种病毒源程序，它们既是钱兴伟的“宠物”，又是他防毒杀毒的利器。 <br />
<br />
　　一次，团里开展网络对抗演练，“蓝军”电子对抗分队的5名“黑客”，用精心准备的病毒包向我指挥网络实施狂轰滥炸。5分钟后，有3台电脑被“黑”，官兵们焦急地看?钱兴伟。钱兴伟却并不?急，神态自若地盯?屏幕上滚动的程序执行命令。 <br />
<br />
　　“驯养”2200电脑病毒 <br />
<br />
　　除了电脑运行的嗡嗡声，指挥大厅静得针落可闻，时钟嘀嗒作响。说时迟那时快，钱兴伟突然开始敲打键盘，劈里啪啦声急促而响亮。一台恢复正常，两台恢复正常，三台恢复正常……10分钟后，被“黑”的电脑全部正常运行。 <br />
<br />
　　钱兴伟又取出一个移动硬盘，插入电脑接口，敲打键盘和移动鼠标的速度越来越快，硬盘“病毒库”中10余种病毒迅速投送到“敌”网络。20分钟后，对抗结束。导调部通报：“敌”方操作系统瘫痪，信息数据链已中断！ <br />
<br />
　　事后，有人好奇地问钱兴伟：“为啥‘黑客’投送的病毒你能制服，你投送的病毒‘敌’无法防御？”钱兴伟说：“他们的病毒是‘下载’版的，专业杀毒软件都能防御；我投的病毒是自家‘养’的，他们从没接触过，当然防不住！” <br />
<br />
　　毒咋能“养”？钱兴伟点开病毒库，打开一个后门病毒的源程序说：“无论什么电脑病毒，实质上都是人为编制的破坏程序。编制病毒程序，或者通过改编使病毒升级，这叫‘养毒’。”一发现有新病毒出现，他总是兴奋不已，必将病毒样本备份到自己电脑中详加研究，对设计精巧、“毒性”奇强的病毒，钱兴伟更是“爱不释手”。 <br />
<br />
　　对抗演练 “毒”建奇功 <br />
<br />
　　有段时间，团里流行一种毁坏word文档的蠕虫病毒。瑞星、卡巴斯基、金山毒霸……这些大名鼎鼎的杀毒软件要么无法杀治。参谋长问怎么办，钱兴伟说：“以毒攻毒！”他用自己“圈养”的一种病毒和蠕虫病毒同放一库，开机运行后，病毒销声匿迹。 <br />
<br />
　　又一次，团队与某基地蓝军分队实施实兵对抗演练，信息对抗难解难分。钱兴伟监控发现，“敌”悄悄在我网络中植入病毒，正源源不断地窃取信息。危急时刻，钱兴伟向团长提出一个大胆的建议：“将计就计，发布假信息迷惑敌人！”最后“敌”在钱兴伟的精心布局下，遭受重创。“一‘毒’胜雄兵！”团长在演练总结时赞叹道。 <br />
<br />
　　钱兴伟“玩毒”8年，玩出了大名堂。他承担或参与开发的8个应用软件和反病毒软件，有两项获全军科学研究成果三等奖，个人荣立三等功2次、二等功1次，获“全军优秀士官人才三等奖”。</div>

							
							
							
							
