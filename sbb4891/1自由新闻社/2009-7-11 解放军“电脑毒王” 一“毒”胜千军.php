<br />
　　蛊在苗族地区俗称“草鬼”，相传它寄附于女子身上，危害他人。那些所谓有蛊的妇女，被称为“草鬼婆”。有苗族学者调查后认为，苗族几乎全民族笃信蛊，只是各地轻重不同而已。他们认为除上述一些突发症外，一些较难治的长期咳嗽、咯血、面色青黑而形体消瘦等，以及内脏不适、肠鸣腹胀、食欲不振等症状为主的慢性疾病，都是着了蛊。属于突发性的，可用喊寨的方式让所谓放蛊的人自行将蛊收回就好了；属于慢性患者，就要请巫师作法“驱毒”了。这种令人生畏的蛊，并非苗人的专利。蛊术在中国古代江南地区早已广为流传。最初，蛊是指生于器皿中的虫，后来，谷物腐败后所生飞蛾以及其他物体变质而生出的虫也被称为蛊。古人认为蛊具有神秘莫测的性质和巨大的毒性，所以又叫毒蛊，可以通过饮食进入人体引发疾病。患者如同被鬼魅迷惑，神智昏乱。先秦人提到的蛊虫大多是指自然生成的神秘毒虫。长期的毒蛊迷信又发展出造蛊害人的观念和做法。据学者考证，战国时代中原地区已有人使用和传授造蛊害人的方法。<br />
<br />
