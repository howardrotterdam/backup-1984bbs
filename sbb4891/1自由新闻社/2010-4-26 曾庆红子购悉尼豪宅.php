April 24, 2010<br />
 <br />
Vote .New laws will make it harder for foreign buyers of Australian real estate, write Jonathan Chancellor and Marika Dobbin. <br />
<br />
It is known as the Tower of Power.<br />
<br />
But one buyer reigns supreme among the high-flyers, celebrities and old money who have taken up residency in the recently completed complex in Clarendon Street, East Melbourne.<br />
<br />
A Hong Kong holding company, directed by Margaret Lou, the little-known businesswoman born in Tianjin, China, paid an Australian record of $19.36 million last month for the 900 square metre apartment. The Bank of China provided the mortgage.<br />
<br />
The sale highlights Australia's resurgent property market, but to some was also emblematic of an apparent raid by foreign buyers.<br />
<br />
Foreign buyers are accused of pushing home prices to dangerous heights in a phenomenon that prime-time current affairs programs have increasingly put to air.<br />
<br />
The public ire caught up yesterday triggering the reversal by the federal government to rules of March last year that relaxed the laws of property ownership in the name of market flexibility.<br />
<br />
The now-abandoned rule changes were initially flagged in late 2008 when Australia was on the edge of recession.<br />
<br />
But the country's economic fortunes have since rebounded, due in part to the remarkable zeal of property buyers.<br />
<br />
So the government moved yesterday to re-regulate the rules on temporary residents who will now have to go through pre-approvals from the Foreign Investment Review Board for property they wish to buy.<br />
<br />
It was billed as a major re-tightening of the market. But there has been an underlying emerging trend towards purchases by parents of international students.<br />
<br />
The number of student family purchases has increased since the March 2009 relaxation C and with the prices that typically exceeded the previous $300,000 house price limit.<br />
<br />
Purchases have included a $9 million harbourfront house in the eastern suburbs which is in the name of three students from the same family.<br />
<br />
Families have been active close to university campuses. At Marsfield, one family bought a two-storey, six-bedroom house for $1.2 million.<br />
<br />
There are more than 434,000 international students studying in Australia, according to the federal Ministry for Education.<br />
<br />
Students from China represented 24 per cent of all overseas students last year. Indian students were second at 18 per cent. No other nationality contributed more than 10 per cent.<br />
<br />
About 37 per cent of all international enrolments are in NSW, and about 30 per cent in Victoria.<br />
<br />
In the absence of more current facts on property purchases, the Herald conducted an informal survey of 30 real estate agents in Melbourne and Sydney for their take on the issue. It found notable differences in our two largest capitals.<br />
<br />
In Melbourne, most agents put the level of overseas buyer interest at about 30 per cent for the eastern belt.<br />
<br />
In Sydney, it was typically put at about 5 per cent, and higher in suburbs popular with migrants, such as Hurstville and Pyrmont.<br />
<br />
A Hurstville estate agent, Maria Fung, from Li &amp; Fung Properties, said about 25 to 30 per cent of her office's sales were to overseas buyers C mainly from China C driven by families buying apartments for their student children.<br />
<br />
&quot;Parents are buying homes for their students studying so they can save money on the increases of rent,&quot; Ms Fung said.<br />
<br />
Anecdotal details have to be taken with a grain of salt. And it is in the interest of agents to talk up the level of demand in the market.<br />
<br />
Somewhat ironically, most agents say that just as public awareness of the issue has peaked, the proportion of overseas buyers has dropped off.<br />
<br />
The strength of the Australian dollar against other currencies, including the Chinese yuan, has also discouraged overseas investors, just as more locals have returned to the market.<br />
<br />
Agents say currency hedging plays a big part in the attraction of Australian property, but it is just one of several factors.<br />
<br />
&quot;Sydney is cheaper than Shanghai,&quot; an Epping estate agent, Lilianna Lou, said.<br />
<br />
The resilience of Australian property compared to other international asset classes during the global financial crisis has no doubt also underpinned the level of buyer interest.<br />
<br />
A property adviser, Barry Jan of BCI Consulting, organises property shopping tours of Australia, and combined migration and real estate investment exhibitions in China.<br />
<br />
He says a booming Chinese middle class is looking to safeguard its wealth in hard assets and the Chinese government is becoming more relaxed about its citizens investing money overseas. &quot;Australia is a favourite for investors because you can own a freehold title, whereas in China you can only lease it,&quot; Mr Jan said.<br />
<br />
Buying Australian property is also a way of advancing migration hopes to Australia. Those who have lodged an application for permanent residency and hold a bridging visa are one of the categories allowed to invest.<br />
<br />
The Chinese oil resources businessman Zeng Wei, and his wife, Jiang Mei, are one example of the opportunities open to foreign investors in the right visa class.<br />
<br />
Mr Wei's father, Zeng Qinghong, was vice-president of China between 2003 and 2008 and the fifth-ranking member in the Politburo Standing Committee.<br />
<br />
In 2008, the couple paid $32.4 million for Craig-y-Mor in Point Piper, the third-most expensive house ever sold in Australia.<br />
<br />
The purchase was made after he obtained a business migration visa the year before.<br />
<br />
The grand 1920s house with renovations by Professor Leslie Wilkinson was initially bought just in his wife's name, as was an earlier acquisition C a $1 million apartment in the World Tower block in Liverpool Street in the CBD in 2005.<br />
<br />
The couple's application to demolish the house and replace it with a new $4.95 million home is being reviewed by Woollahra Council, which is seeking heritage advice.<br />
<br />
Chinese buyers currently in the market have told agents they are seeking FIRB approval to spend up to $40 million on a harbourfront house in the eastern suburbs.<br />
<br />
Professor Graeme Hugo, from the national centre for social applications of geographic information systems at the University of Adelaide, says globalisation has blurred the formerly distinct lines of nationality, making protectionism increasingly irrelevant.<br />
<br />
He says Australia has a new model of migration where people tend to belong to more than one country.<br />
<br />
&quot;There are many property buyers from Hong Kong, China and Taiwan who share their time between countries, keeping a foot in both,&quot; he says.<br />
<br />
&quot;We still tend to think in terms of the model of a migrant moving here permanently, like the European migration of the 1950s and '60s.&quot;<br />
<br />
Dual citizenship could be one driver behind tales of mansions that attract attention if they are left empty for years. Of course, land banking is not peculiar to overseas buyers, and has long been practised by the local rich.<br />
<br />
Professor Hugo's comments point to the nub of the debate on foreign buyers C population growth.<br />
<br />
Estate agents recall that in the 1980s there was the biggest property boom in living memory, which correlated with a rapid jump in the net gain from overseas migration, from about 100,000 to a peak of 150,000 in 1988-89.<br />
<br />
The current property market's high auction clearance rates and soaring prices are following the same pattern, further fuelled by strong internal population growth.<br />
<br />
So it appears that much of the frustration about foreign buyers could be misplaced. Even if the real estate agents' numbers are correct, such buyers are still a minority in the market. Interstate and overseas migration, combined with Australia's baby boom, are the powerful contributors to the housing shortage.<br />
<br />
Victoria's population alone grew by 117,900, or 2.2 per cent, in the year to September, just ahead of NSW at 117,000, according to the Bureau of Statistics.<br />
<br />
Victoria's net overseas immigration was 82,055, or 70 per cent of the state's population growth for the same period, with 30 per cent from natural increase and interstate migration.<br />
<br />
Professor Hugo says overseas-born Australians, once settled, have a higher home ownership rate than Australian-born citizens.<br />
<br />
The 2006 census indicated that migrants who had arrived between 2002 and 2006 had only a 30 per cent home ownership rate.<br />
