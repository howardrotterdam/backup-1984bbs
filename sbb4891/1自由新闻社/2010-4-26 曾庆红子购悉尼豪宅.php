													<h2>译文：悉尼先驱晨报：热的发烫的房地产市场</h2>
						
						
																											<div id="postmessage_584781" class="t_msgfont">新法律让外国买家购买澳大利亚房产将更为困难<br />
<br />
作者： JONATHAN CHANCELLOR AND MARIKA DOBBIN<br />
发表时间：2010年4月24日<br />
译者、校对：@xiaomi2020<br />
<br />
<br />
这被称为“权力之塔”。<br />
<br />
<br />
但是一位买家在众声喧哗之中占到了巅峰，那些在东墨尔本（Melbourne）的克拉伦登（Clarendon）街新完工的住宅区中入住的影视明星和昔日大亨们都甘拜下风。<br />
<br />
<br />
上个月，由Margaret Lou，一位鲜为人知的生于中国天津的商业女性领导的一家香港的控股公司，以$1,936万澳元【译注：约合RMB1.22亿】，全澳洲创纪录的最高价购买了900平方米的公寓，由中国银行提供按揭。<br />
<br />
<br />
这桩交易是澳大利亚房地产市场复苏中的亮点，但对某些人来说，也是外国买家入侵市场的明显征兆。<br />
<br />
<br />
在最近播放的占据了黄金时段的时事节目中，外国买家被认为是推高地产价格达到危险高度的罪魁祸首。<br />
<br />
<br />
昨天公众的愤怒触动了联邦政府，将去年三月在市场自由调节的名义下对房地产所有权的相关法律松绑重新收紧。【相关阅读：<a href="http://www.ausproperty.cn/market/comment/2457.html" target="_blank">联邦政府加强监察澳洲房子的外国买家</a> 】<br />
<br />
这些法规调整是2008下半年，当澳大利亚在衰退的边缘时出台的，现在已经被废弃。<br />
<br />
<br />
但是国家的经济已经回暖，部分要归功于房地产买家的高涨的热情。<br />
<br />
<br />
于是政府昨天开始定下规则，重新规范，临时性居民如想购买房产，必须经过外国投资审查委员会（Foreign Investment Review Board）的预先批准。这被认为是市场的重大的重新收紧措施。但是国际学生的家长们已经引发了一股暗地涌动的购买热潮。<br />
<br />
<br />
从 2009年三月的限制放宽以来，学生家庭购买房产的数量就在日益增长，而且通常都超过了之前设下的$30万澳元的房价上限。<br />
<br />
<br />
这些交易中就包括了在东部郊区一座售价为$900万澳元位于港口的水景房，这栋房子是以同一个家庭的三名学生的名义购买的。<br />
<br />
<br />
学生家庭对于大学校园周边的房产也颇为热心。在马斯菲尔德（Marsfield），某户购买了一栋两层的带六个卧室的房子，售价是$120万澳元。<br />
<br />
按照联邦教育部的统计，在澳大利亚有超过43.4万的国际学生。<br />
<br />
<br />
去年，从中国来的学生占了所有海外学生的24%。印度学生紧随其后，占到18%。其他国籍的学生都没有超过10%。<br />
<br />
有将近37%的国际学生都在新南威尔士州（NSW）入学，有近30%在维多利亚（Victoria）。<br />
<br />
在缺少当期房地产交易数据的情况下，《晨报》对墨尔本和悉尼的30家房地产中介进行了一次非正式调查，了解他们的大致情况。结果发现这两大重点城市有着显著的不同。<br />
<br />
<br />
在墨尔本，大多数的中介认为海外买家中有30%对东部地带有兴趣。在悉尼，只有约5%，对移民来说，郊区，如Hurstville和Pyrmont区域，有更大的吸引力，。<br />
<br />
<br />
一家Hurstville的地产中介，来自于Li &amp; Fung地产的Maria Fung说，她的公司有25~30%的销售来自于海外买家―主要是中国―都是为了孩子上学而买房的家庭。<br />
<br />
<br />
Fung女士说，“家长们为了他们到此就学的孩子而买房，这样他们就可以在租金上扬的时候省钱。”<br />
<br />
传奇般的细节听起来都需要有所保留。而鼓吹市场正在变热也符合中介们的利益。<br />
<br />
<br />
但是讽刺的是，大多数的中介说当公众对这一问题的关注度正在提升的时候，海外买家的比例却下降了。<br />
<br />
<br />
澳元对其他货币，包括对人民币的走强阻碍了海外买家，同时也让更多的澳洲本地人回归市场。<br />
<br />
<br />
中介们说汇率保值成了澳洲地产的重要吸引力，但是这不过是几个因素之一。<br />
<br />
<br />
来自Epping estate中介的Lilianna Lou说，“悉尼的房子比上海便宜。”<br />
<br />
和其他国际资产市场相比，澳洲地产在全球金融危机中的反弹无疑也支撑了买家的热情。<br />
<br />
<br />
BCI 咨询公司的Barry Jan，一位地产顾问，组织了一次澳大利亚购房之旅，将移民和房地产投资在中国打包展览。<br />
<br />
<br />
<strong>他说中国蓬勃发展的中产阶级正在寻找投资于实业的财富避风港。中国政府也对其国民的海外投资放宽了限制。“澳大利亚受到投资者的青睐因为你可以终生保有房产，而在中国你只能租用。”Jan先生说。购买澳大利亚房产也是进一步希望移民到澳洲的方法。那些已获得永久居住申请，且有往返签证的人也是获许投资的一类人。中国的石油商人曾伟和他的妻子蒋梅就属于这一类在寻找机会的外国投资者们。【相关阅读：蒋梅曾为中央电视台《影视同期声》的主持，这是她的<a href="http://www.cctv.com/tvguide/anchors/jiangmei/index.html" target="_blank">央视介绍</a> 。】<br />
<br />
曾伟的父亲，曾庆红，从2003年到2008年任职中国国家副主席，是政治局常委排名第五位的人物。<br />
<br />
<br />
2008年，这对夫妇花了$3,240万澳元，购买了Point Piper区的著名豪宅Craig-Y-Mor，也是澳洲第三昂贵的房子。<br />
<br />
<br />
这桩交易是他在2007年获得投资移民身份后做出的。<br />
<br />
<br />
这栋20世纪20年代建造的房子由Leslie Wilkinson教授重新翻修，和上一次，2005年购买利物浦（ Liverpool ）街位于CBD的世界大厦，价值$100万澳元的公寓一样，曾伟的此次购买也以他的妻子的名义完成。<br />
<br />
<br />
这对夫妇申请推翻这一建筑，重建一栋价值$495万澳元的豪宅，该申请正在由Woollahra委员会审核，考虑是否要寻求遗产保护。</strong><br />
<br />
<br />
房地产市场上的中国买家们已经告诉中介，他们在等待FIRB的准许，购买东郊的一栋港口水景房，价值$4,000万澳元。<br />
<br />
<br />
来自于阿德莱德大学（University of Adelaide）地理信息系统的社会应用国家中心的Graeme Hugo教授说全球化已经模糊了之前界限分明的国籍差别，让保护主义变得相对无关。<br />
<br />
<br />
他说澳大利亚有着移民的新模式，人们倾向于不仅属于一个国家。“许多来自于香港、中国和台湾的地产买家在多个国家生活，每个地方都有参与。”<br />
<br />
<br />
“我们还是以为移民的模式是永久地移到这里来住，如同20世纪50年代和60年代的欧洲移民。”<br />
<br />
<br />
双重国籍可能是豪宅神话背后的牵引力，即使这些豪宅可能多年来都空置。当然，土地储备对于海外买家并不引以为奇，很久以来本地富豪就在这么做了。<br />
<br />
<br />
Hugo教授的评论点出了围绕着海外买家的辩论的实质――人口增长。地产中介回忆道，在80年代，人们还记得出现了最大的一次地产繁荣的热潮，与海外移民的快速增长相关，到1988-89年时从约10 万人达到了高峰期的15万人。<br />
<br />
当前房地产市场的高出清率和高扬的价格遵循了同样的模式，同时被强劲的内部人口增长进一步地催热。<br />
<br />
所以看起来很多关于外国买家的负面评论是错误的。即使地产中介的数据正确，这种买家还是市场上的少数。州际间的移民、海外移民、加上澳洲的婴儿潮，这些都是形成房屋短缺的背后原因。<br />
<br />
<br />
按照统计局的数据，到9月份，维多利亚（Victoria）地区的人口一年就增长了117,900人，或2.2%，刚好领先于新南威尔士州（NSW）的117,000人。<br />
