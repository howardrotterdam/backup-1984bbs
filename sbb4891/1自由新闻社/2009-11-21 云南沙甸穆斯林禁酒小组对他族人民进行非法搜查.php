<br />
我是一个土生土长的沙甸人，也是沙甸志愿者的成员，也参加过很多次沙甸的禁酒活动，我看你们“揭露、批判”已经好多天了，我在此代表沙甸全体人民向你们说一声“谢谢，你们有心了”。<br />
本来我是不想说什么的，但是看你们一直在这里无休止的争论，觉得有必要说明一些情况，让你们和那些真正关心沙甸的兄弟姐妹们明白，沙甸不会做有损伊斯兰的事，过去、现在、以后都不会。<br />
我也声明，我发帖不是想也不愿意和您们理论什么，我只是说说事实，至于相信与否，您们自己定夺。&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;<br />
1.&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;我们从来没有非法闯入民宅：<br />
在你们看来我们是一群野蛮人，但是我们也懂法，我们历来是先敲门并说明来意得到允许之后才进入非穆斯林的出租屋，让派出所协查，就让派出所监督我们在活动中有没有违法，以便我们时时规范自己。<br />
<br />
2.&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;我们从来没有在非穆斯林家里翻箱倒柜，强行搜查：<br />
我们的成员在参加活动前都是接受过学习的：在进门之后不得碰禁酒对象家的一草一木、一针一线。<br />
<br />
3.&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;我们从来没有强制性禁止非穆斯林朋友饮酒、吃肉：<br />
对在沙甸辖区租房内发现饮酒、吃肉的朋友，我们是先交流、善待再交流、再善待，如果是因为腰酸背痛需要喝药酒的，我们用云南白药和他交换，甚至帮他就医。在这里要提一下，沙甸是由西营、金鸡寨、川方寨、冲破哨、团破、团破头、新沙甸、莲花塘、凤尾村几个紧紧相邻的自然村组成，其中在冲破哨、团破居住的就是非穆斯林，但是他们饮酒、吃猪肉我们从不干预，哪怕是他们家离清真寺很近。<br />
<br />
4.&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;我们从来没有想过针对非穆斯林同胞：<br />
禁酒的对象包括沙甸本地居住穆斯林、外来务工人员（穆斯林、非穆斯林）注：外来务工人员都是居住出租屋，沙甸的房东有对求租户有宣传并让其知道沙甸有禁忌的事宜，如果租住户在不知情的情况下违规，我们责成的是房东而不是租住户<br />
<br />
5.&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;我们从来没有把那一位违反我们禁忌的朋友强行赶出沙甸<br />
针对沙甸本地穆斯林的饮酒者我们是各个清真寺相互通告：不为饮酒者办理红白两事：（按伊斯兰的教法：结婚除了有政府部门的证明、证书外还得有阿訇主持的仪式，不然婚姻也被视为无效，死人得通过殡礼仪式才能入土），不过还好，现在沙甸本地的饮酒者已经积极配合了我们的禁忌活动，至少不会在村子里饮酒。<br />
对于交流、善待N次仍然不尊重我们的外来务工人员饮酒者（非穆斯林、穆斯林），我们会让其自由选择，一，尊重我们，不在我们的村子吃饮我们所禁忌的就可以。如果他从外面喝得大醉也与我们无关，二，离开我们村子，去附近没有禁忌的村子租住。如果选择第二者，我们安排车子帮他们免费搬家，经济困难的租住户，我们补贴一个月租金。<br />
<br />
如果我们真的像你们所说的那样是“非法侵入住宅”，我可以明确的告诉你们，我们的禁忌活动不会也不可能一直做到现在，已经快三年了。请你们不要怀疑政府部门的公正执法，也不要侮辱那些来沙甸务工的非穆斯林同胞们的智商和学识，他们也懂法，他们也会用法，也不要误认为他们是软弱和无能，他们不是，他们很坚强，他们很能干，沙甸今天的发展离不开他们，他们只是多了一些尊重，我们喜欢他们，我们也需要他们。<br />
沙甸没有禁酒以前2007年因为酗酒发生的刑事案件143起（其他本地人因素17起，外来务工人员因素126起），但是禁酒至今发生5起（外来务工人员因素）。<br />
这些天的争论也是因为一个好出风头的小兄弟把活动内容放在沙甸志愿者内部论坛上，也不知是那位朋友把帖子转了出来，还在各网站广为流传，然后您们就一直孜孜不倦的“揭露、批判”，真是辛苦你们，不过说真的，我们还是要谢谢您们，原先我们也没有想过大肆宣传沙甸的禁酒活动，因为我们没有什么远大理想。以前只是很少一些穆斯林同胞知道我们禁酒，现在就好了，知晓的人越来越多了（包括许多非穆斯林），反正沙甸要改走旅游路线，先来个“一举成名”何尝不是好事，至少有很多很多人知道了中国有一个地方叫“沙甸”，今天为止，我们接到了从全国各地打来询问打电话273个，其他还有一些是非穆斯林同胞，当他们了解了我们禁酒的相关事宜后也表示了理解和支持，反而谴责了那些凭空捏造、无事生非的人。<br />
如果有朋友认为我是在欲盖弥彰的话，您可以亲自来落实，明察也好暗访也行，有必要我们可以提供车旅费和食住宿，要是觉得还是继续这样“揭露、批判”有意义，就请继续吧，但是我希望大家明白一点：我们禁酒是教法因素，请您们不要把它政治化，您们可以不尊重我们，但是您们得尊重我们的信仰。至于谁是分裂民族团结，谁是挑衅民族事端的人，大家心里清楚，我们同样也知道什么是诽谤罪，我们同样有使用法律的权利。<br />
