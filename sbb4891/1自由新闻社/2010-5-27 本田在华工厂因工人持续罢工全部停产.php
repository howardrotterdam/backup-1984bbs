　　昨天，本田零部件公司总务科人士向记者证实了辞退两名员工一事。但他表示，辞退他们是因为二人“行为过激、违反公司规定”，并表示，宣布辞退的时候，有当地政府、劳动仲裁部门、工会等各方人士在场，并且公司已经掌握了员工违反规定的证据，按照公司规则，“唆使员工集会、停工怠工”都属于违反公司规定。<br />
