<br />
中国江苏网5月27日报道 今日有媒体报道称，由于本田在广东佛山的零部件工厂持续罢工，本田在中国的所有四家组装工厂已被迫全部暂停生产。此前，本田中国相关负责人承认，“本田零部件公司工人停工已经对合资企业生产造成影响。”<br />
<br />
<br />
记者今日就此事致电广州本田公司，工作人员向记者证实了广本工厂暂停生产的消息。据了解，本田中国组装厂的汽车变速器全部来自已罢工的佛山零部件工厂，由于供应中断，广州本田在增城和黄埔的两家工厂从25日起已全部暂停生产。至于何时恢复生产，对方称一切要等到佛山工厂劳资纠纷解决才能有答案。<br />
<br />
而记者从东风本田客服处得到的答案是，目前武汉工厂生产一切正常，并未停工。客服代表让记者留下联系方式，称随后将联系我们。<br />
<br />
据了解，本田在佛山的零部件工厂工人因不满薪资待遇从5月7日开始罢工，要求厂方将薪水提高至2000~2500元。此后，本田零部件公司先后提出两种加薪方案，但均未达到工人要求而遭到拒绝。截止记者发稿时，本田零部件工厂工人罢工仍在持续中。<br />
