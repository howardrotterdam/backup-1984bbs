<br />
本田I级工资=基本工资(675元)+职能工资(340元)+全勤补贴(100元)+生活补贴(65元)+住房补贴(250元)+交通补贴(80元)=1510元，扣除社保金后，到手的工资为1211元；若除去房租及日常生活费用，每月仅剩456元。<br />
<br />
