																											<div id="postmessage_643301" class="t_msgfont">相关报道：广东本田工人停工因加薪慢 中日员工薪酬差50倍<br />
<br />
&nbsp; &nbsp; 本田汽车零部件制造有限公司位于广东佛山南海狮山工业园区，总投资9800万美元，于2007年3月正式投产，是本田在海外建立的第4家可全工序生产自动变速箱的工厂。<br />
<br />
　　据媒体报道，南海本田的员工不仅工资低，而且加薪缓慢。其薪酬体系分为五大级别，一级是最低级别，五级是最高级别，每个大级别下面有15个小级别，共75个级别，每年评审一次，合格后就晋升一级，等于升一大级工资要15年。有的员工，3年涨了111元，还是老员工。<br />
<br />
　　而在南海本田，科长及以上级别管理人员均为日方人员，以部长为例，每月收入可达10万元人民币以上。员工向媒体反映，“日本那边经常会派支援者过来南海厂，支援者吃、住、行全包，每天还有300多美金的补助，相当于普通工人两个月工资。”中日员工工资相差了50倍。[详细]<br />
<br />
&nbsp; &nbsp; 什么是罢工？<br />
<br />
&nbsp; &nbsp; 在国际社会，罢工制度是伴随着产业工人运动的兴起和蓬勃发展而逐渐形成的。目前，多数工业国家都通过宪法对罢工权进行了规定。1946年法兰西共和国宪法规定“罢工之权利在法律规定范围内行使之”，1947年意大利共和国宪法第40条规定“罢工权应在调整此项权利的法律范围内行使之”，1978年西班牙宪法第28条规定“承认劳动者为保卫自身利益举行罢工的权利”，1946年巴西联邦共和国宪法第158条规定“罢工权应予以承认，其行使方式以法律规定之”。<br />
<br />
&nbsp; &nbsp; 我国于2001年经全国人大常委批准加入的《经济、社会及文化权利国际公约》第八条，要求缔约国承诺保证公民“有权罢工，但应按照各个国家的法律行使此项权利”，对此，我国未作保留，罢工权为我国公民的当然权利。<br />
<br />
　　在俄国革命后，提出了一个新的问题：在工人国家中罢工工人是“对自己举行罢工吗？”列宁在1921年论证说，“在无产阶级执政的国家里采取罢工斗争，其原因只能是无产阶级国家中还存在着官僚主义弊病，它的机关中还存在着各种资本主义制度旧残余”。（《列宁选集》第4卷，第585页）。<br />
<br />
　　毛泽东则提出了罢工权入宪的主张，认为：“要允许工人罢工，允许群众示威。游行示威在宪法上是有根据的。以后修改宪法，我主张加一个罢工自由，要允许工人罢工。这样，有利于解决国家、厂长同群众的矛盾。”（毛泽东：《在中国共产党第八届中央委员会第二次全体会议上的讲话》，1956年11月15日。）<br />
<br />
　　从宪法权利的层面讲，政治权利和自由，包括政治权利（选举权和被选举权）以及政治自由（言论、新闻、出版、结社、集会、游行、示威、罢工）。在产业社会初期，罢工的原因既有经济性原因，也有政治性原因。而当下我国的各类罢工主要是因劳资纠纷导致的经济性纠纷，即使是罢工涉及了政治意见的表达，其表达形式也包含在言论、出版、集会、游行、示威等政治自由中，没有规定专项法律对其进行规范和指引。这不得不说是政治自由的一种缺失。<br />
