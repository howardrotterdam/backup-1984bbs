													<h2>佛山本田工人高喊&quot;罢工到底&quot; 要求重整工会</h2>
						
						
																											<div id="postmessage_643982" class="t_msgfont">佛山本田工人高喊&quot;罢工到底&quot; 要求重整工会<br />
2010-05-28 05:21:35　来源: 新京报(北京)　跟贴 333 条 手机看新闻<br />
<br />
核心提示：昨日下午，位于广东佛山南海狮山工业园的广东南海本田汽车零部件制造有限公司工人戴着口罩，身穿工作服，数次从厂区排队走出，他们高喊着“罢工到底”的口号，要求提高待遇，并重新签订劳动合同。他们还要求，重整工会，重新选举工会主席和相关工作人员。<br />
视频说明：佛山本田工人高喊&quot;罢工到底&quot;，要求重整工会。<br />
<br />
本田在华工厂因工人持续罢工全部停产<br />
本田在华工厂因工人持续罢工全部停产。 来源：IC<br />
<br />
本田在华工厂因工人持续罢工全部停产 <br />
<br />
本田在华工厂因工人持续罢工全部停产，加薪方案遭工人拒绝 来源：IC<br />
<br />
新京报5月28日报道&nbsp;&nbsp;昨日，记者从本田中国、广汽本田和东风本田方面证实，因受旗下配套零部件工厂劳资纠纷的罢工影响，上述三家整车企业的四个组装厂已经全部被迫停产。广汽本田昨日傍晚发表声明称，地方政府已经介入调停，劳资双方正积极协商问题解决办法。记者从厂家和北京地区经销商了解到，目前罢工事件还未对市场销售产生影响。<br />
<br />
四个整车组装厂停产<br />
<br />
据了解，本次“罢工门”事件的缘由是，本田在佛山的零部件工厂工人因不满薪资待遇，从5月17日开始罢工，除中间恢复上工一天，罢工一直在持续。他们要求厂方将薪水提高至2000-2500元。<br />
<br />
成立于2005年的本田汽车零部件制造有限公司是本田的独资公司，为本田在华企业提供汽车变速箱及发动机零件，目前年产变速器48万套。<br />
<br />
据广本内部人士介绍，由于本田在华的四个整车组装厂一直采用同步配套至生产线的方式生产，因此在零部件工厂罢工发生后，组装厂因缺少库存而被迫停产。本田中国证实，在变速器断产后，本田中国在广州负责出口的组装厂，广汽本田在增城及黄埔的组装厂，以及东风本田在武汉组装厂全线停产。<br />
<br />
“受本田汽车零部件制造有限公司停工影响，部分零部件供应短缺，广汽本田对生产日程进行了调整：24日中班、25日、26日、27日、28日暂停生产。”广汽本田表示，何时复工目前不得而知，不过地方政府已经介入调停，劳资双方正积极协商问题解决办法。东风本田相关负责人也表示，工厂从26日晚开始停产，暂定停产两天，但具体何时恢复生产，还得看零部件公司劳资双方的谈判情况。<br />
<br />
工人要求重整工会<br />
<br />
据媒体报道，5月22日，两名带头停工的工人遭本田零部件公司辞退，本田零部件公司当天通过广播向所有员工宣布辞退两名带头停工的工人。<br />
<br />
据《南方日报》报道，昨日下午，南海本田零部件厂工人戴着口罩，身穿工作服，数次从厂区排队走出，他们高喊着“罢工到底”的口号，要求提高待遇，并重新签订劳动合同。他们还要求，重整工会，重新选举工会主席和相关工作人员。<br />
<br />
另有工人在接受媒体采访时指出，除了提高薪酬的要求外，还要求公司改善薪酬机制和变革管理制度。<br />
<br />
据了解，该厂共有1600多名工人，而实习生数量超过一半。<br />
<br />
工人拒绝补贴方案<br />
<br />
本田零部件公司曾先后提出两种加薪方案，但均未达到工人要求而遭到拒绝。<br />
<br />
本田零部件制造有限公司目前已经在25日提出给员工涨55元补贴的基础上，再次提高补贴标准。<br />
<br />
按照新方案，公司实习满3个月的实习生每月在原来的基础上增加477元；一级员工和二级员工基本工资涨200元、生活补贴加35元、伙食补贴加 120元。广州本田零部件公司相关人士昨日表示，该公司已向员工传达报酬改善方案并尽快建立员工意见收集机制，使员工高层间更顺畅沟通。<br />
<br />
但据了解，上述补偿方案并没有得到罢工工人的认可。记者从广州本田零部件公司获悉，罢工还在继续，工厂仍处于停产状态。<br />
<br />
本田三家整车有关负责人昨日在接受记者采访时指出，这次罢工事件已经对旗下企业的生产造成了影响。同时表示，一旦问题解决，本田方面将会通过调整生产日程和节奏的方式来挽回欠产。另外有本田合资公司内部人士对记者表示，在零部件工人工资上涨后，本田方面将负责消耗成本上涨的压力，不会涉及合资公司产品。<br />
<br />
■ 看法<br />
<br />
“中日员工应同工同酬”<br />
<br />
对于本田零部件公司罢工事件，北京大学经济学院劳动经济学专家夏业良认为，这反映了中国工人，尤其是外方独资企业的劳工普遍缺乏保障的社会性问题。夏业良认为，中日双方员工如此巨大的工资差距绝对是不合理的，公司必须给出对这一差距以及中方员工的低薪充分解释，应该同工同酬。<br />
<br />
“罢工，实在是工人被逼而迫不得已为之。工人不仅承受着巨大的折磨和心理压力，还要面对罢工后的巨大风险。”夏业良表示，国外工会制度以及工人权益保障机制都十分完善，而中国在这方面还有不小的差距。“工会应发挥其作为与企业谈判主体的强大职能，保障职工权益。”<br />
<br />
■ 影响<br />
<br />
市场销售暂未受影响<br />
<br />
本田在华遭遇的“罢工门”事件虽然导致了整车厂的停产，但三家整车企业相关负责人和旗下经销商昨日在接受记者采访时均表示，目前市场销售暂未受到影响，经销商处还留有足够的库存车辆。<br />
<br />
广汽本田相关人士表示，广本一直以来实行零库存的策略，因此厂家在停产后工厂内没有库存车。“但广汽本田特约销售服务店均保持有合理的库存，产品销售及服务不受影响。”广本昨日发布的声明称。<br />
<br />
东风本田的相关负责人表示，目前东风本田员工的薪酬水平在行业中处于正常水平，东风本田内部也尚未发生过劳资纠纷。“与零部件公司属于本田在华独资公司不同，广汽本田和东风本田均是中外合资公司，中方也有同样话语权，员工权益也相应更有保障。”<br />
<br />
此外，记者从经销商处了解到，停产暂未影响到市场终端销售。目前，广汽本田、东风本田在京的经销商处现车均十分充足，且由于正值销售淡季，广州本田经销商的库存较此前稍有增加。不过，东风本田一位经销商的相关负责人表示，未来一段时间有可能会出现车源供应紧张情况，车主若需更换一些比较少用的零配件，等待时间也可能会加长。<br />
<br />
■ 链接<br />
<br />
工人晒薪水<br />
<br />
