　　表态<br />
　　记者了解到，停工事件发生后，本田零部件公司、本田中国公司与佛山市南海区狮山镇政府及劳动部门进行了沟通。在解决方案再度被员工否决、停工继续的状态下，昨天，上述各方首次共同对外释放了“将会 妥善解决此事”的信号。<br />
　　记者昨天联系到了佛山市南海区狮山镇宣传教育和社会事务办公室，一位工作人员在电话中告诉记者，本田零部件公司属于外商独资企业，政府在停工事件中只能起到协调作用，“我们不会偏袒任何一方，一切要以法律为依据。”<br />
