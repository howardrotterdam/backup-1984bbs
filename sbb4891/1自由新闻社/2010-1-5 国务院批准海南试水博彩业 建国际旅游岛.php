													<h2>国务院批准海南试水博彩业 建国际旅游岛</h2>
						
						
																											<div id="postmessage_414560" class="t_msgfont"><a href="http://news.163.com/10/0105/02/5S7TVMA7000120GU.html" target="_blank">http://news.163.com/10/0105/02/5S7TVMA7000120GU.html</a><br />
核心提示：国务院四日发布《关于推进海南国际旅游岛建设发展的若干意见》，同时中央给予海南投融资、财税、土地、行业开放等诸多政策支持。《意见》指出，将在海南试办一些国际通行的旅游体育娱乐项目，探索发展竞猜型体育彩票和大型国际赛事即开彩票。<br />
<br />
中国国务院四日发布《关于推进海南国际旅游岛建设发展的若干意见》，将海南建设国际旅游岛上升为国家战略。意见提出，海南国际旅游岛建设发展六大战略定位，即中国旅游业改革创新的试验区，世界一流的海岛休闲度假旅游目的地，全国生态文明建设示范区，国际经济合作和文化交流的重要平台，南海资源开发和服务基地，国家热带现代农业基地。图为海南三亚的游艇码头。 （中新社发 尹海明 摄）<br />
<br />
中新社1月4日电 国务院四日发布《关于推进海南国际旅游岛建设发展的若干意见》(下称《意见》)，为海南未来发展明确了方向，同时中央给予海南投融资、财税、土地、行业开放等诸多政策支持。<br />
<br />
中央赋予海南省西部大开发政策。在基础设施、生态建设、环境保护、扶贫开发和社会事业等方面安排中央预算内投资和其他有关中央专项投资时，海南享受和西部一样的优惠政策。在投融资政策上，中央还支持符合条件的旅游企业发行企业债券，设立旅游产业投资基金等。<br />
<br />
旅游业进一步开放。实施更加便利的出入境管理措施，增加芬兰、丹麦、挪威、乌克兰、哈萨克斯坦五国为入境免签证国家，至此海南免签证国家达二十六国。俄罗斯、韩国、德国等三国的旅游团组团人数放宽至二人以上(含二人)，入境停留时间延长至二十一天。支持海南引进国内外大型旅游企业，培育旅游骨干企业和知名品牌。<br />
<br />
西沙旅游有望获得突破。《意见》指出，要积极稳妥推进开放开发西沙旅游，有序发展无居民岛屿旅游。《意见》强调，依法加强西沙和无居民岛屿管理，按照属地管理原则依法进行土地确权登记。<br />
<br />
旅游业发展和娱乐业密切相关。《意见》指出，将在海南试办一些国际通行的旅游体育娱乐项目，探索发展竞猜型体育彩票和大型国际赛事即开彩票。<br />
<br />
国际旅游岛建设需要土地政策支持。《意见》同意海南在不突破国家下达的耕地保有量、基本农田保护面积和建设用地总规模的前提下，试行对土地利用总体规划实施定期评估和调整机制。开展城乡建设用地增减挂钩试点、农村集体经济组织和村民利用集体建设用地自主开发旅游项目试点。业内专家表示，此举使海南拥有相对灵活和自主的土地使用权。<br />
<br />
在扩大市内免税购物方面，针对目前全国只对境外旅客离境退税，海南有望将销售对象延伸到所有离岛旅客，包括境外和国内旅客。《意见》指示由财政部牵头研究在海南试行境外旅客购物离境退税的具体办法和离岛旅客免税购物政策的可行性，另行上报国务院。<br />
<br />
在财税政策支持上，《意见》表明，中央财政将加大对海南的均衡性转移支付力度，加大对海南在其他一般性转移支付和专项转移支付的支持。中央财政在一定时期内还将对海南国际旅游岛的建设发展给予专项补助。<br />
<br />
为加快国际旅游岛建设，中央还对海南农业、工业、软件业、服务业等其它产业发展给予支持。《意见》指出，要把海南建成南海油气资源勘探开发服务和加工基地。适时规划建设国家石油战略储备基地，鼓励发展商业石油储备和成品油储备。</div>

							
							
							
							
