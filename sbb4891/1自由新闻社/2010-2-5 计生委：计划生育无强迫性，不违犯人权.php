　　首先，计划生育政策的出台完全没有民意基础。1980年中共中央发表了《关于控制我国人口增长问题致全体共产党员共青团员的公开信》：“为了争取在本世纪末把我国人口总数控制在十二亿以内，国务院已经向全国人民发出号召，提倡一对夫妇只生育一个孩子。”是谁要求了我国人口一定要控制在12亿以内的？是广大人民要求的吗？显然不是。因此，控制人口增长的政策是完全没有民意基础的，相反的是，全国人民是“被要求”控制人口增长的。由此可见，中国官僚的思维就是人民是可以而且是必须“被要求”的。官僚们不是去满足人民的需要，而是要求人民去满足它们提出的“目标”。没有民意基础，不经过民主决策，这是官僚思维的第一个特征。<br />
　　<br />
　　第二，计划生育政策的出台完全没有经过科学的论证。多少人口才算是合适的人口？为什么要控制在12亿以内？经过了什么科学论证？计划生育政策完全无视性别比例失调、老龄化、独生子女心理问题以及所有这些问题导致的个人、家庭和社会影响。此外，计划生育政策也完全无视推行此项政策所带来的社会冲突以及推行此项政策所需的人力物力等社会成本。没有可行性，不经过科学论证，以想当然作为政策基础，这是官僚思维的第二个特征。<br />
　　<br />
　　第三，计划生育政策完全把人口当作负担。“人口增加，除了家庭需要增加抚养费以外，为了解决他们的上学、就业等问题，国家还需要增加教育经费、设备投资和社会公用事业经费等。”（引自《公开信》）每个人都有从小到大的过程，需要有一个抚养和教育的过程，但是当他长大成才之后就能回馈家庭和社会了。一个人是否成为家庭和社会的负担，就是要看他对家庭和社会索取与回馈的相对多少。如果他为家庭和社会创造的财富多过他消耗的财富，那么他就对家庭和社会做出了贡献，相反才是对家庭和社会的负担。笼统地把增加的人口当作增加的负担，显然是错误的。把人民当成他们需要摆脱的负担，而不是社会财富的创造者和他们的衣食父母，这是官僚思维的第三个特征。<br />
　　<br />
　　第四，计划生育政策逻辑混乱。计划生育的逻辑基础是，财富总量不变，人口越多则平均财富越少，人口越少平均财富越多。计划生育的口号是“少生快富”。实际情况是，在一定的劳动生产率条件下，财富的多少是与劳动力人口数量正相关的。目前中国的财富远远多于实行计划生育的30年前，人口是增加了而不是减少了。然而，今后由于计划生育政策导致的人口老龄化，劳动力人口将逐年减少，尽管劳动生产率在不断提高，但中国还将面临经济快速衰落的巨大风险――除非大量从非洲引进黑人劳工。财富的增加要依靠人们的创造，而不是对现有财富进行分赃式的分配――参与分赃的人数越少就分配越多。另外，计划生育政策还对农村和少数民族实行与城镇居民不同的政策，其逻辑显然是自相矛盾的。既然计划生育利国利民，能够“少生快富”、“少生优育”，那为什么却不让少数民族“快富”和“优育”而实行与城镇居民一样的独子政策呢？难道是故意不让少数民族过上富裕的生活吗？这显然不是政策制定者的初衷吧。实际上，不让少数民族实行计划生育是照顾他们。这就很奇怪了，既然搞计划生育可以“少生快富”，为什么不让少数民族“少生快富”却反而是照顾他们呢？如果说，计划生育是不好的政策，不对少数民族实行计划生育是对的，那么，为什么说对汉族实行计划生育却又会是好的政策呢？逻辑混乱，缺乏基本常识，这是官僚思维的第四个特征。<br />
　　<br />
　　第五，计划生育政策已经造成严重后果。计划生育造成的严重后果，比如性别比例严重失调、人口结构严重失调、快速与严重的老龄化问题、独生子女心理问题及各种家庭悲剧、未来人口骤降的可见经济与社会悲惨前景，等等，计划生育决策者们一概看不见。无视决策失败的后果，不承担决策失败责任，只知道一味自我陶醉于伟大光荣与正确，对批评者只知道一味压制与打压，这是官僚思维的第五个特征。<br />
　　<br />
　　可见，分析计划生育政策，可以充分认识典型的中国官僚思维特征。这种官僚思维的特征就是：罔顾民意、唯我独尊、不讲科学、逻辑混乱、视民如寇、死不认错、不负责任。</div>

							
							
							
							
