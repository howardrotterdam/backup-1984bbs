<br />
她说，与发达国家相比，中国育龄妇女的人工流产率与美国基本接近，中国是28％，美国是25％。<br />
<br />
中国从70年代初开始实行计划生育政策，通过提倡“晚婚晚育，优生优育”，在城市中推行鼓励独生子女的政策，农村的生育政策则相对宽松，在少数民族聚居区还实行各地政府根据实际情况制订相应计划生育政策的原则。<br />
<br />
