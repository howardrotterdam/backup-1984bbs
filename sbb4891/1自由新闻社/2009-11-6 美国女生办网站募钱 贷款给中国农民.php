																											<div id="postmessage_316250" class="t_msgfont">美国女生办网站募钱形成贷款投给中国农民 <br />
 <br />
2009年10月29日 来源：青年周末 <br />
&nbsp;&nbsp;<img src="http://i38.tinypic.com/osema8.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
孟康妮和魏可欣（右）用网站作为小额信贷的平台<br />
　　用五十元钱改变一个家庭<br />
<br />
　　美国女生办网站帮中国农民找钱<br />
<br />
　　◎文/《青年周末》记者 陈七妹 ◎图片由被访者提供<br />
<br />
　　46岁的张琼华的孩子一个上大学一个上高中。她想借2000元人民币买两头小猪仔来养；五云高娃48岁，种四亩地，养了50头山羊，还种了1300棵防火树。她想借4000元钱，卖点草种，再给树林修个篱笆；格日勒想贷款4000元钱，投入到奶牛身上，赚钱了将来把喜欢摔跤和足球的儿子送到市里的体育学校。<br />
<br />
　　数百名来自内蒙古和四川的农民，正在网上等待小额贷款。<br />
<br />
　　■两个热情的美国妞儿<br />
<br />
　　帮中国农村的穷人找贷款，是美国女孩魏可欣这两年半以来一直在忙的事。<br />
<br />
　　她和同伴创办的我开网开创了一种新慈善：通过网络把人们手里的几十元、几百元钱集中起来，投资给养牛户、养鸡户和小店老板做生意。关键的是，这些贷款不求回报，不为升值，甚至一去不复返，永远处在放贷的循环之中。<br />
<br />
　　她和我开网如何把借钱顺理成章地变成慈善捐款？<br />
<br />
　　“这两个美国妞哪儿来这么大的热情？”一位中国网友对美国女孩魏可欣和孟康妮两人的行为表示不解。<br />
<br />
　　这两个有中国名字的美国女孩在一年多前，一起创办了我开网(wokai.org)。网站目标非常直接：把网友的零钱集中起来，形成每笔数百美元的低额贷款，专门投给中国农民。<br />
<br />
　　至今，我开网上的636位投资人已经为20多位内蒙古赤峰市和四川仪陇地区的贫困农民投了10万美元。魏可欣是网站CEO，孟康妮是董事之一。<br />
<br />
　　魏可欣和孟康妮两个美国女孩跟中国内蒙古农村的奶牛养殖户之间并无任何关联，但她们用自己的“哲学”将二者联系起来：“我生在一个能给我很多机会的世界中，我可以上大学、做任何我想做的工作。但是不是每个人都有这样的机会呢？所以我得付出，帮助那些没有这种机会的人。”<br />
<br />
　　魏可欣和孟康妮三年前结识于清华大学，在中国的日子里，她们都给自己取了中文名字、学会了中文，还开始关注中国的经济发展问题。孟康妮2006年曾经在一个小额信贷的机构工作过，魏可欣则一直对清洁能源和经济发展有浓厚的兴趣。两个人一拍即合，“我开网”诞生了。<br />
<br />
　　“我开”这个名字的意思就是，让农民开始做自己的生意，来改善家庭的状况翻译成英文是“I start”。<br />
<br />
　　“我三年前来到中国，”魏可欣接受记者的电话采访，基本上，她每次开始介绍的时候，都会先说在她看来非常重要的信息：“中国2亿贫穷人口平均每天生活费都不足1.25美元。中国的城市富裕、发展非常快，但是农村落后太多。小额的信贷对于农村来说太重要了，这可能是改善贫困农民生存状况的有效方法……他们需要帮助，我们需要做一些事情去改善贫穷。”<br />
<br />
　　■网络理想国的投资<br />
<br />
　　我开网上的这10万美元并非一般意义上的投资：有的人一次只拿出了10美元或者20美元；资金即便被归还，投资人也得不到回报。这些钱会在不同的农民手里继续滚动，为不同的小生意出力。<br />
<br />
　　“对于投资人来说，这更像一个善事，而不是投资。”<br />
<br />
　　魏可欣向记者解释我开网的运作过程。网友通过免费的网上支付工具Google check out把钱打到我开网的账户上，再由我开网把钱转给在内蒙古和四川的两个合作机构，最终由合作者们负责发钱、收钱。借出去的钱还回来之后，再由捐款者选择下一个需要钱的农民，如此周而复始。<br />
<br />
　　这听上去简直比“天使投资”还天使。为什么会有这样的“雷锋”？<br />
<br />
　　看看我开网上的636个投资人的资料吧。几乎都是年轻的面孔，有在世界各地的华裔、中国人、有喜欢中国的、和在中国生活的外国人。<br />
<br />
　　在美国加州的台湾小伙子艾伦拿出了555美元资助四川农民邓军民。邓军民需要钱来开一个修理和出售农具的小铺子。邓军民2009年5月18日开始借款，现在已经还款37%。<br />
<br />
　　艾伦写道：“中国农村的经济发展不仅能帮数亿贫困人口走出困境，也能为解决全球的贫穷问题做贡献。我受过最好的教育、有我的美国梦。中国人充满希望，有很好的生活态度，有充沛的精力，能帮他们，是我这样一个人所能做的！”<br />
<br />
　　一位google公司的员工里奇拿出20美元，和另外16个人一起投了600美元(约合4000人民币)给赤峰农民赵秀琴，赵秀琴用这笔钱为修理店买了一台机床。<br />
<br />
　　“我看过我开网的宣传录像，这真是个好点子！”里奇简单的投资理念代表了大部分网友轻松的慈善心态。<br />
<br />
　　我开网在纽约、旧金山、西雅图、香港和北京有150多名志愿者，他们利用休息的时间为“我开网”做宣传演讲、办PARTY，来扩大网站的影响，有的人自己就是投资者。包括NBC、《亚洲新闻》、《新闻周刊》等主流媒体都关注到“我开网”这个新生事物和它的运营模式。<br />
<br />
　　■农民都是“银行家”<br />
<br />
　　魏可欣把我开网注册在美国，因为在中国，不经过中国人民银行允许，任何单位和个人都不能从事金融业务。因为种种政策上的原因，魏可欣想了很多变通的办法。其中包括所有的投资都必须是美元，通过美国转回国内。<br />
<br />
　　农户能按时还钱吗？钱被用在什么地方了？不还款怎么办？<br />
<br />
　　面对小额信贷，几乎所有人都会有疑问，更何况是由一个网站来做。魏可欣找到了解决信任危机的办法。<br />
<br />
　　“我们有非常严格的管理和评价体系。一旦我们选定合作伙伴，就去看他们最近三年的财务数据，是否有重要的社会影响力、看他们的管理团队是否具有集资和增强机构的能力，还得看他们是不是个透明的组织。然后我们要下去做三到五天的一个实际接触，从组织的领导到管理人员再到出纳、会计、放贷员等都要谈话，最后要看他们的贷款客户的审计，比如从3000个贷款客户中选五到十个去面谈。CZWSDA需要钱，我们可以帮他们筹到。”<br />
<br />
　　内蒙古昭乌达妇女可持续发展协会(以下简称CZWSDA)是我开网的合作伙伴之一。他们负责将钱送到赤峰农民的手中。CZWSDA秘书长霍桂林在接受记者采访时介绍说， CZWSDA由联合国和赤峰妇联合办，已有十多年历史，以发放小额贷款为主要工作，至今回款率达到了99.9%以上。<br />
<br />
　　CZWSDA的工作人员负责收集农民的信息，为他们拍照，把他们的信息公布在我开网上，并且每三个月就负责报道一下进展，最后收集还款。<br />
<br />
　　“可欣和康妮，她们两个人给人的感觉特别敬业。”这是霍桂林对两个美国女孩的印象。<br />
<br />
　　为了巩固这个关于“信任”的联盟，霍桂林和他的团队也在努力。<br />
<br />
　　每五个村民形成一个担保组，互相监督，谁还不上钱，大家来帮助。<br />
<br />
　　每一笔钱的花销都会及时更新在网上：草种长出来了吗？新买的小牛出栏了吗？家里有了什么变化？<br />
<br />
