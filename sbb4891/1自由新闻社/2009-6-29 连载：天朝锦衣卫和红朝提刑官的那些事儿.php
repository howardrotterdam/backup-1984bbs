<br />
现在的话量不多，但他以前的东西都还是写的蛮有意思的，这哥们儿住北京，以前在锦衣卫署干过两三年，当然，杀人越货的勾当么干过，倒是后来去了刑总技术大队，整天价见死人。。。<br />
<br />
<br />
至少是他保证的，会更新，不过劝您别报太大希望。。。<br />
<br />
<br />
<br />
锦衣卫的那些事儿 <br />
<br />
<br />
两个问题<br />
<br />
当锦衣卫的那段时间，接触到很多人，他们中的绝大多数都会问我两个问题：<br />
1，长安街逆行过么？<br />
2，暗杀过人吗？<br />
<br />
很是众口一词。<br />
似乎全国的锦衣卫，不干这两件事情的，就不正宗。<br />
就跟王麻子剪刀剪不断铁丝就不正宗一样。<br />
<br />
我每次都耐心的回答，没有，两件事情都没有。<br />
<br />
实际上有没有呢？<br />
<br />
也没有。<br />
<br />
长安街逆行，的确是红牌特别通行的特权，但是，没人这么做，因为没必要。<br />
至于暗杀，亲军都尉府有专门的部门，可惜，我不是该部门的成员。<br />
<br />
―――――――――――――――――――――――――――――<br />
<br />
<br />
当年有意见轰动一时的事情，北京不少人都知道。<br />
该事情的直接后果就是，北京市公安局巡警总队，解散。<br />
<br />
起因是，东单赛特商场门口，一辆奥迪a6改装车违章停车，车内的四人聚众打牌。<br />
某巡警上前制止，对方出示了锦衣卫工作证件，示意特别公务。<br />
巡警继续制止，并且强迫锦衣卫开走。<br />
被四名锦衣卫制服，扔进了后备箱。<br />
<br />
四人将车开至前门东大街9号内停泊，各自回家。<br />
次日，才想起后备箱中有人，将该巡警放出。<br />
<br />
时任巡警总队总队长闻讯大怒，命令所有外勤车辆一律开至亲军都尉府门口，鸣笛。<br />
<br />
时任亲军都尉府京都锦衣卫署都统闻讯亦大怒，命令所有锦衣卫外勤车辆一律开至巡总大门口鸣笛。<br />
<br />
至此，京师舆论哗然。<br />
<br />
时任同知枢密院事号令有伤风化，成何体统，勒令各部门车辆就地解散，两主管罚俸两年。<br />
<br />
次年，巡总解散，至此，京师所有巡警，皆为其他衙役充任。<br />
<br />
锦衣卫之特权，可见一斑。<br />
<br />
――――――――――――――――――――――――――――<br />
<br />
<br />
中国国家安全部的牌子和公安部挂在一起，都在北京市东长安街14号，但办公地点还是在中央调查部大院。所以国家安全部队外都称中直机关大院。在北京海淀区颐和园附近，北京中医研究院南门向西100米。国家安全部大院主要分两大部分，一个是办公区，一个是家属区。家属区只要有正规的理由，一般是可以进出的。至于办公区，必须要特殊证件。<br />
<br />
以上为百度搜索得到。<br />
<br />
同理，亲军都尉府京都锦衣卫署，名义上的办公地点在前门东大街9号，也跟北京市公安局挂在一起。<br />
实际上的办公地点，也不在那里。<br />
那里是一个接待处，对于锦衣卫系统之外人，必须先在那里登记注册，然后等待审批，同意之后，将发给你一个临时通行证，然你前往通行证所在地址。<br />
却不一定是亲军都尉府京都锦衣卫总署所在地。<br />
<br />
也许只是一个安全岛，也许是你所要办理的业务所在地，更有可能的是……<br />
不同意，直接驳回。<br />
<br />
外界接触锦衣卫系统，是很困难的。<br />
这造成了锦衣卫形象的神秘，以及社会对这类人群普遍的误解。<br />
<br />
我前面已经说过，他以前是锦衣卫，现在是技术警。虽然，怎么说呢，他是先写了这个的，但是呢，你不还得讲个“先来后到”不是？后传么，后来当技术警的传么。。。<br />
<br />
<br />
你好，我是技术警<br />
<br />
<br />
事先说明，事情的发生未必在我身上，或者我同事身上，但是为了增加趣味性，人称上做了适度修改。<br />
请勿认真。<br />
顺便祭天。<br />
啥时更新。<br />
这可没准。<br />
<br />
――――――――――――――――――――――――――<br />
<br />
<br />
刑总第七支队，就是技术支队。<br />
我曾经在七支队直属机构，海淀区青龙岗子的法医中心实习三个月，在刑总工作前后加起来两年不到。<br />
说起来，也算一个技术警。<br />
技术队的事情，还是很好玩的。<br />
<br />
就从那个永不磨灭的传说开始吧……<br />
<br />
<br />
出案发现场，是刑警的家常便饭。<br />
但是，为了保护现场，第一个进去的，必需全部是技术警，只有我们才能挪动尸体和收集证物或者勘察痕迹，其他警察，警戒线外面站着去，当然可以问问周围群众口供和排查过往车辆什么的。<br />
在我们没完事之前，闲杂警察，一律免进。<br />
<br />
接到报案，一哥们死在游泳池里了。<br />
技术警赶到现场，因为是晚上，露天游泳池，池底很黑，怕破坏现场，他们没下去，而是紧急调来了探照灯。<br />
我说过了，在技术警没完事之前，其他警察都不能进来。<br />
<br />
灯来了，大功率的，“啪”一打开，就听见里面的技术队兄弟哈哈大笑，有个家伙大吼一声：“卧槽，吸毒啊。”<br />
外面的哥们就愣了，忙用电台问发现了什么？是不是需要缉毒支队增援。<br />
里面就是笑，根本不理，于是大家都呼啦啦都进去了。<br />
然后都笑崩了。<br />
<br />
<br />
那哥们是从十米跳台上蹦下来的，游泳池水被排掉了，他保持了入水前的动作，加上大头朝下，脖子被拍进了胸腔，趴在游泳池底，俨然就是一只蛤蟆。<br />
刚才的“吸毒”实际上是“西毒”。<br />
而且最神奇的，出血不多。<br />
我后来见到过东成西就和功夫里面蛤蟆功的造型，说真的，都没那个地道。<br />
星爷和王大哥真应该看看记录这个案子的卷宗。<br />
<br />
<br />
――――――――――――――――――――――――<br />
<br />
<br />
一天发现一河漂，也不知道泡了多久，发的厉害，整个人足足400多斤，圆鼓囊囊的，根本没法解剖，一刀下去，都是气和脓水，臭的厉害，通风橱里都不行。<br />
我们弟兄都是久经考验的，全废了，跑出去深呼吸，太臭了。<br />
然后老大来了（支队长是老大，政委是老三-其实是老二，但老二不是还有别的意思吗，于是就三儿了），看了看，说：“拉走。”<br />
于是抬到了地下室。<br />
有个好大的离心机，老大一脚把那哥们踹进去了，盖盖，启动，叮咣一通乱想，排液口就开始流脓水，我们足足接了50升的开口桶两桶。<br />
那叫一个臭啊！<br />
<br />
然后不怎么出了，再把里面已经转碎了的样本拿出来，拼在一起，找死亡原因和测定死亡时间什么的。<br />
