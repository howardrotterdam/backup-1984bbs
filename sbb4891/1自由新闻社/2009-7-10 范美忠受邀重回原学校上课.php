梁文道：为什么真小人也想当英雄 <br />
<br />
<br />
&nbsp; &nbsp; 真小人居然成了人格典型。一个人再多妄语、再多劣行都不成问题，只要他够真就行了，仿佛“真”就是最了不起的价值。<br />
<br />
&nbsp; &nbsp; 斯坦福大学东亚系教授王斑在《历史的崇高形象》中文版的前言如是说：“此书是从八十年代末至九十年代中期酝酿、思考写成的，因而染上了那个时期浓重的躲避崇高、反崇高的情绪……如果我现在有机会重写这部书，我的倾向会跟原来不一样……这时代，反崇高已经跌落到了迷恋自己的肚脐眼以下，落到了肉身的吃喝拉撒睡的底层，走向了反面。” <br />
<br />
&nbsp; &nbsp; 我想很多人也会有同感。曾几何时，中国的主导意识形态充满了一种崇高美学，标榜超乎常人的理想、道德与激情，大家不只要学雷锋“毫不利己，专门利人”的精神，甚至还要胸怀改变全世界的大志。然后到了上世纪九十年代，英雄的传说变成专供调侃的笑话，崇高的价值变成空大的假话。很多人开始觉得敢于承认自己卑微自私甚至无赖的人物才是英雄。问题是经过近二十年来反崇高反英雄的潮流之后，我们真的走出了崇高逻辑的桎梏了吗？还是跌入了同类逻辑的反向发展呢？ <br />
<br />
&nbsp; &nbsp; 四川都江堰市光亚学校的老师范美忠在自己的博客里坦承，地震当天，他不顾学生，先跑出教室，于是在网上招来一片唾骂。当大部分人都在谴责他时，也有人为之辩护，说他不只有说话的自由也有自私逃命的权利。有趣的是这场争论再发展下来，竟然又成了一次崇高与反崇高的对决，英雄和真小人的较量。<strong>范美忠随后发表的言论更是把它上升到了自由和非自由之争的理论层面，俨然把自己变成了“自由主义”的代表。 <br />
<br />
&nbsp; &nbsp; 以我的粗浅理解，的确没有哪一路自由主义会否认一个人逃命求存的权利，但似乎也没有任何一种理论会认为一个人必须在危难关头抛下学生亲友自求多福。舍己救人是崇高的。几乎在任何文化里面，几乎在任何一种伦理学的立场来看，这都是值得敬佩的行为。 </strong><br />
<br />
&nbsp; &nbsp; 范美忠身为北大历史系毕业生，自愿到地方上的小学校任教，并且常常为文探讨教育体制的缺失，实在也是种教育理念的实践；他的逃跑乃是一时软弱，是人类求生本能的表现。或许不符他以前的高尚言行理想形象，但也不值得大家的峻词辱骂。毕竟英雄不是人人都做得到的，所以英雄才格外让人尊敬。然而，我们为他凡人的软弱扼腕甚至辩护，却不表示我们佩服他的行为。更不表示这是每一个有人性的人都该效法的事。范美忠是个真正的凡人，不多不少如此而已，既不邪恶亦不高尚。为何有些人会把他捧成令人尊敬的人物？为何他自己又显示出好像要为他所理解的“自由主义”奋战，当个人权斗士的倾向呢？ <br />
<br />
&nbsp; &nbsp; <strong>由此可见，反对崇高反了二十多年，许多人仍然没有逃离崇高的逻辑，不做英雄就做反英雄，总是要和一套宏大的叙事挂钩，总是要让自己变成伟大理论的道成肉身。传统的英雄观鼓吹超乎常人的伟大尺度；现代的反英雄则标榜真小人的真实，似乎自私和软弱不只是人之常情，以为人人都该如此，不只虚浮甚至虚假。可是过去二十年来，对这种虚假宣传的反感却居然使得真小人成了很吸引人的人格典型。一个人再多妄语、再多劣行都不是问题，只要他够真就行了，仿佛“真”就是最了不起的价值。<br />
<br />
&nbsp; &nbsp; 我们躲避过度的崇高，是为了复现人性的本来的面目。认识了人性的怯懦与卑下，我们才懂得包容和悲愤，再踏实地谋求个人道德上的进步和完善；而不是反其道而行，奉怯懦卑下为理想。在举国歌颂英勇义士的气氛下，范老师的坦白不应惹起如此愤恨，反而更该让我们t悟那些英雄的伟大超凡。反过来说，范老师也完全没必要引用什么自由主义的主张来掩饰一时的本能表现。这要不是为了贯彻自己自由主义的信徒的形象，就是另一种当英雄的冲动，一种反英雄的冲动。更何况自由主义虽然以人性的缺陷为其前提之一，却断不会把它当成终极的目标。&nbsp;&nbsp;</strong><br />
