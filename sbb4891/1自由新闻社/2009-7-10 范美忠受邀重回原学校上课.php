<br />
地震中逃跑教师范美忠重回原学校上课<br />
2009-07-10 14:07:53　来源: 中央人民广播电台经济之声　跟贴 718 条 手机看新闻<br />
<br />
核心提示：7月8号记者获悉，“范跑跑”已经重新回到地震前任教的光亚学校上课，为学生讲解文学。范美忠说，自己回到学校是受到校长卿光亚的邀请。卿光亚还说，其实在地震当天，“范跑跑”的行为没有任何问题，因为他的学生是预科学生，有行为能力。<br />
<br />
<br />
范美忠 资料图片<br />
<br />
中国广播网成都台7月10日报道&nbsp;&nbsp;都江堰光亚学校的老师范美忠在地震发生那一刻，弃学生于不顾第一个跑出教室，人送绰号“范跑跑”。范美忠因此倍受争议。7月8号记者获悉，目前，“范跑跑”已经重新回到光亚学校上课，为学生讲解文学。<br />
<br />
范美忠说，自己回到学校是受到校长卿光亚的邀请，而且是为大学预科的孩子们上课，摆脱了高考制度的束缚，他觉得很自由，想怎么上就怎么上。那么，为什么卿光亚会让一个倍受争议的人再次回到课堂呢?<br />
<br />
卿光亚还说，其实在地震当天，“范跑跑”的行为没有任何问题，因为他的学生是预科学生，有行为能力。而且学校廊道旁都贴着《紧急情况条例》：第一，离开建筑;第二，不要携带任何财物;第三，不要寻找你的朋友;第四，靠右行，到达空旷地点;第五，静静等待命令，直到宣布解散。老师这样做了，他没有错。<br />
<br />
那是什么地方出了问题呢?为什么按照正常程序进行的行为会掀起这么大的波澜呢?也许从范美忠自己的回答中，我们可以找到些许答案：“我不需要回避，我承认我的软弱，我并没有觉得这有什么见不得人的事情。我的生命受到威胁，我将首先保护我的生命。我并不认为我的生命比学生重要，但是我也不认为学生的生命比我重要。”<br />
<br />
让时间倒带，地震之后，很多人感受到的是牺牲、光荣、感动这样的字眼所带来的情绪，但“范跑跑”把自己的行为通过网络公布出来，他的目的就是“要让你们看看现场是多么无序，个人多么无力和胆小”。一个人与一群人的对立，矛盾冲突显而易见。<br />
<br />
好在，人们还是很宽容：他并不是说完全丧失了人性，只是说在当时很大灾难的面前，他肯定是一时也没反应过来。<br />
<br />
社科院教授胡光伟也说：“任何人都有在世界上生存的权利，对于公民来讲，给他一个工作，应该是最起码的，在市场经济里，作为公民来讲，是可以自由选择的，对学校来讲可以自由选择老师，家长也可以自由选择老师。 (本文来源：中央人民广播电台经济之声 作者：王欢) 杜青翰</div>

							
							
							
							
