&nbsp; &nbsp; 卿光亚还说，其实在地震当天，“范跑跑”的行为没有任何问题，因为他的学生是预科学生，有行为能力。而且学校廊道旁都贴着《紧急情况条例》：第一，离开建筑；第二，不要携带任何财物；第三，不要寻找你的朋友；第四，靠右行，到达空旷地点；第五，静静等待命令，直到宣布解散。老师这样做了，他没有错。<br />
&nbsp; &nbsp; <br />
