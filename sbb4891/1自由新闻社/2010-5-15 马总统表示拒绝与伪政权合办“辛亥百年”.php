																											<div id="postmessage_617416" class="t_msgfont">台海网5月15日报道　马英九今天表示，为展现“建国”百年意义、价值与气势，“建国百年”活动要办的像是台湾博览会，尽善尽美。大陆要举办辛亥革命展览，大家各办各的，“我们当然不会去”。<br />
<br />
据台湾“中央社”报道，“中华民国建国一百年庆祝活动筹备委员会”上午举办第2次全体委员会议，马英九出席致词。<br />
<br />
马英九指出，大陆要举办辛亥革命展览，“我们当然不会去”。目前政府的作法，就是大家各办各的。辛亥革命当然很重要，但最重要的是，辛亥革命推翻了满清，建立民国，“我们现在‘建国’一百年了，这才是我们最重视的一项工作。”在这一百年当中，明年“中华民国”已经在台湾62年了。<br />
<br />
他说，“中华民国”在台湾的过程，就整个华人世界来讲，是前所未有的发展，包括自由民主的制度、多元开放的社会，都是非常了不起的成果。大家不只要缅怀过去，也要面对现在，策励将来，政府必须让人民有信心，可以让大家的未来比现在更好。尤其在愿景规划方面，给民众更大信心，展现出真正的台湾精神。<br />
<br />
马英九期许“建国百年”庆祝活动，“一定要尽可能的办到尽善尽美，展现我们‘建国’百年的意义、价值跟气势，并且对未来一定要具有前瞻。”<br />
<br />
马英九也希望，“建国百年”活动，可以当作为期1年的“台湾博览会”，与大家的生活结合在一起，而不是只建1个馆或展览1样东西，而是将“建国百年”的许多价值、精神融化在全民生活中。<br />
<br />
他呼吁，大家不分党派、不分族群、不分政治颜色都来参与，展现台湾重要的特色，包括自由、和平与繁荣。<br />
<br />
马英九指出，日前他与民进党主席蔡英文进行的辩论，双方都很用心，表现也不错。重要的不是谁输谁赢，而是真正给台湾的民主加分，这正是台湾精神所在。<br />
<br />
他希望，“建国百年”能呈现国际化，过程中可以展现更多的包容，“‘建国百年’也不一定要是一言堂，可以容许不同的想法。”<br />
<br />
马英九也希望，民间政府或社团学校可以发起邀请国际人士来台湾玩的活动，“我们家办喜事，百岁的生日，请到我家来，为我们庆生。”以这样的心情邀请国际人士参加，很自然地把台湾行销出去。<br />
