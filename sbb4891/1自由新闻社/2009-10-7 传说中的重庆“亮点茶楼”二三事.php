　　　　关系在哪里？靠山是谁？通过江湖人士，她们很快与当时重庆重量级人物――文副局长拉上了关系。<br />
　　<br />
　　　　随着时间的推移，“亮点”越做越大，客源如潮。为何？其中最主要的原因不在于收费低廉、服务到位。有网友说：亮点，让男人找到了尊严。<br />
