28日，市委书记薄熙来与张轩、范照兵、刘光磊、刘学普看望慰问打黑除恶一线干警，并与政法系统同志座谈。薄熙来指出，我市“打黑除恶”专项斗争，是在中央的统一部署下进行的，要始终坚持依法办案、实事求是，做到不枉不纵，经得起历史的检验。<br />
