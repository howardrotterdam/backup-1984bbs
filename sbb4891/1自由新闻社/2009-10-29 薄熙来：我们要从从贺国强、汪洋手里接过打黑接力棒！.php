重庆涉黑团伙遭受毁灭性打击<br />
<br />
刘光磊介绍说，开展打黑除恶专项斗争以来，重庆市黑恶犯罪举报中心共收到25187份检举、揭发和控告黑恶犯罪的材料，其中80％是实名举报，为打黑除恶工作提供了大量有价值的线索，“真正打了一场打黑除恶的人民战争”。<br />
<br />
他说，打黑除恶专项斗争战果突出、成效明显：一是严厉打击了各类黑恶势力。“长期盘踞我市的重大涉黑团伙受到毁灭性打击”。二是净化了社会治安环境。三是为重庆经济发展创造了良好的社会环境。四是化解了一大批社会矛盾。<br />
<br />
重庆市检察院院长余敏介绍说，截至目前，重庆共立案查办在打黑除恶斗争中暴露出来的职务犯罪案件47件52人，其中涉及贪污贿赂犯罪案件44件49人，涉及县处级以上要案20人，厅级干部10人，涉及政法干警29人。<br />
<br />
余敏介绍说，重庆检察机关已批准逮捕814人，其中以涉黑罪名批捕385人，起诉涉黑涉恶案件36件327人。今年以来，全市检察机关以涉黑罪名批准逮捕 385人。备受社会关注的陈坤志、陈明亮、陈知益、岳宁、岳村等重大涉黑团伙案件已进入检察机关审查起诉阶段，将于11月上旬陆续提起公诉。<br />
<br />
打黑不搞“从重从快从严”<br />
<br />
刘光磊表示，重庆市委、市政府从一开始就要求打黑除恶必须坚持实事求是，严格依法办案，保证把每起涉黑涉恶案件都办成经得起法律和历史检验的铁案。中央政治局委员、市委书记薄熙来特别强调“破案要敢于碰硬，办案结案要过硬”。“打黑除恶要坚持实事求是、严格依法办事，打击面既不缩小、也不扩大；定性上既不压低、也不拔高，做到不枉、不纵、不漏”。“要坚持法制、程序和规范，不搞“从重从快从严”。<br />
<br />
重庆建立了案件质量保障机制，公安机关实行办案质量终身责任制度、错案责任倒查制度，严把执法办案的每个环节、每个关口，坚决防止发生冤假错案。重庆市检察院要求全市各级参与此项工作的检察机关，确保全市批准逮捕、提起公诉的黑恶势力犯罪案件有罪判决率达到100％。<br />
<br />
刘光磊说，打黑除恶是一项长期的战略任务，不可能毕其功于一役，要始终坚持“露头就打”，及时发现、打击涉黑涉恶犯罪和黑恶势力“保护伞”，使打黑除恶的工作形成常态。<br />
<br />
不剥夺涉黑被告的应有权利<br />
<br />
重庆市高级人民法院院长钱锋表示，法院审理涉黑案件没有特殊性，仍然坚持严格的法定程序，按照现行的法律规定，遵循基本的司法规律来进行。“不是因为它是涉黑涉恶案件我们就要另外设定程序，另外制定标准。”<br />
<br />
他说，高院专门强调了依法、公正、文明、高效四个规定。目的是真正把每一个涉黑涉恶案件都办成经得起法律检验，经得起人民检验，经得起历史检验的铁案。<br />
<br />
他说，只有同时在组织特征、经济特征、行为特征、非法控制4方面具备黑社会性质组织的特征，法院才会予以认定，“既要防止将涉黑涉恶案件犯罪作为一般刑事犯罪或者社会治安案件来对待，又不能为了显示成绩而扩大打击面”。<br />
<br />
他说，判决会严格遵守法定程序，严守审判中立，绝不能因为被告人的社会危害性和公众的仇视心理而剥夺被告人的应有权利，法院会充分告知被告人享有的各项权利。<br />
<br />
他说，在已经开庭审理的案件中，不少被告人对黑社会性质组织犯罪表示不认罪，或者对公诉机关起诉的其他罪名和犯罪事实不予认可。“这是任何一个刑事案件审理过程中都可能出现的问题，作为法官而言，这是不足为怪的。这也正说明了在涉黑案件审判中，被告人及其辩护人的表达权、辩护权得到了充分保障。”(本报记者田文生 中青在线－中国青年报 本报重庆10月29日)</div>

							
							
							
							
