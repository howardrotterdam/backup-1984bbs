<br />
　　打黑除恶是个硬碰硬的斗争，也有如毛主席所言，这“不是请客吃饭，不是做文章，不是绘画绣花，不能温良恭俭让。”我们政法战线给大家发了枪支警具干什么？是因为有敌对势力，要维护社会秩序。我们工作中要谦虚谨慎，但绝不能对黑恶势力温柔放纵。我们温柔，黑恶势力不会温柔，而且要杀人的！对少数人的宽容，就是对大众的不公、不负责任。<br />
<br />
黑恶势力连渣场、快餐、“小面”、零售这些小行当也不放过，困难群众怎么生活？<br />
<br />
　　薄熙来说，黑恶势力向各行各业渗透，不仅在金融、交通、物流、工程、建筑、土地征用等大行当中实施暴力垄断，就连渣场、快餐、“小面”、零售这些小行当也不放过。困难群众本来就收入微薄，一个月挣不了几个钱，走到哪儿再碰上这种人欺行霸市，怎么生活？我们一些干部认为在这些小行当“打黑除恶”没必要，价值不大。这真是饱汉不知饿汉饥！是否需要“打黑除恶”，不能以某个行业的涉案金额高低来决定。对低收入群众来说，那些小行当虽然挣钱不多，却关系到他们的生存。我们一定要带着对人民群众的感情来办案。<br />
<br />
　　没有清明的社会环境和严谨的法治环境，就不可能有现代经济的蓬勃发展<br />
<br />
　　薄熙来说，有人认为，“打黑除恶”会把各路客商都“吓跑”，会影响经济的发展，“只有包容才能发展”。这种说法是假“深刻”，至少是“杞人忧天”。恰恰相反，一个地方，只有具备了清明的社会环境，严谨的法治环境，才能吸引真正有本事、走正道的公司和生意人，放心来投资发展，地方经济才有希望，才能最终繁荣起来。任何犯罪嚣张、黑恶横行的地方，绝不会有现代经济的蓬勃发展。重庆两个月前，刚签订了两个大项目，能形成2000亿元的产值，达产后，出口额将比目前全市出口额加起来还要翻番。今年全市引进外资也在去年基础上翻了一番。良好的社会和法治环境，是从根本上促进经济发展的基础条件。<br />
<br />
　　群众讲，这些警察是真正的警察，对得起这身警服！有这样的同志，重庆前途无量！我深信邪不压正，还是好人多呀！<br />
<br />
　　薄熙来说，这次打黑除恶，咱重庆涌现出一批先进典型。全市人民对他们倍加尊重，也通过他们看到了我们公检法的正义形象。群众讲，这些警察是真正的警察，对得起这身警服！听到这句话，工作干到这份儿上，人民发自内心的肯定和感谢，何其珍贵！就是赴汤蹈火、粉身碎骨也该心满意足了！这次成立了200多个专案组，7000余名公安干警和武警官兵走到了第一线，夜以继日，冲锋陷阵。“五加二”、“白加黑”，每天工作15小时以上，经受了大考验。市法院、检察院的同志，一个案子，十几卷、几十卷、几万字的起诉材料，都得消化吸收，确实不易！我来重庆时间不长，为有这些同志而自豪，有这样的同志，重庆前途无量！尽管我们队伍里有腐败分子，有见利忘义之徒充当“保护伞”，我深信邪不压正，还是好人多呀！市公安局程明同志，连续辗转于10个区县，每天工作17小时，最后因公殉职。渝北区民警杜廷江连续工作70多天，劳累过度，经抢救无效英年早逝！这场斗争之后要好好立个碑，寄托我们的哀思！<br />
<br />
　　张德邻、贺国强、黄镇东、汪洋先后四位书记，打黑除恶态度鲜明，而且力度大，工作实，这也像跑接力，一棒接一棒<br />
<br />
　　薄熙来强调，我们历任书记、市长对这项工作都高度重视。重庆直辖以来，张德邻、贺国强、黄镇东、汪洋四位书记，蒲海清、包叙定、王鸿举几位市长，都对打黑除恶态度鲜明，而且力度很大，工作很实。张德邻同志1996年就提出，对黑恶势力要采取“从速、从重、从严”的方针。2001年贺国强同志强调，“对黑恶势力必须‘嫉恶如仇’，坚决予以打击，尤其对涉嫌‘保护伞’问题，不管涉及到谁，都要一查到底，依法严惩”。今天，我们不用多讲别的，就把国强同志当年讲的话多重复几遍就管用！镇东和汪洋同志都讲，要对黑恶犯罪保持高压态势，要形成强大震慑。2001年到2005年，全市打掉黑恶犯罪团伙17个；2006到2007年，全市打掉恶势力团伙251个，打击处理违法犯罪人员1790人，规模不小。打黑除恶的斗争在继续，这就像跑接力，从德邻、国强、镇东和汪洋同志手中一棒接一棒，现在轮到我们了，决不能中断！一定要有共产党人的浩然正气和无所畏惧的牺牲精神，把打黑斗争进行到底。<br />
<br />
　　讲打黑除恶，重庆要好好向广东、湖北和上海等兄弟省市学习<br />
<br />
　　薄熙来要求，要认真向兄弟省市打黑除恶斗争学习。广东省今年开展了“雷霆09”打黑除恶的专项行动。他们起名“雷霆”，确有雷霆万钧之势，出动了警力84000多人次，打掉各类犯罪团伙1341个，抓获犯罪嫌疑人8139名，比重庆抓的要多很多，还缴获了枪支185支，成绩很大。湖北省掀起了打黑除恶风暴，斗争的锋芒直指盘踞街道小区的“街霸”，横行集贸市场的“市霸”，垄断强占建筑、运输、娱乐等行业的“行霸”，出没于乡村街路的“地霸”等。一年多打掉了恶势力团伙611个，犯罪成员4164人，还成立了打黑专业队。上海2000年以来进行了三次大规模的打黑除恶专项行动。今年10月，又进行了反腐打黑总动员，公布了举报电话，实行责任倒查追究制度。重庆要好好向兄弟省市学习。<br />
<br />
　　对每个案子的性质，不压低也绝不拔高；对团伙涉案人员，不缩小也绝不扩大。就是四个字：实事求是<br />
<br />
　　薄熙来强调，专项斗争一定要坚决，同时一定要依法办事，一定要稳步实施，要稳、准、狠。法制是现代文明的重要体现，公安、检察、法院都要严格依法办事。打黑的过程也是法制教育和实践的过程。每个环节，每个程序，都要严格依照法律，要经得起历史的检验。对被告人应当享有的辩护、申请回避等诉讼权利，不能因其罪大恶极而被剥夺。审理过程中既要体现威严，也要体现文明。<br />
<br />
　　他说，依法办案，实事求是，是我们的总体要求。实事求是是我们党根本的思想路线。所以我们提出，在办案过程中对每个案子的性质，不压低也绝不拔高；对团伙涉案人员，不缩小也绝不扩大。简言之，不压低、不拔高；不缩小、不扩大。怎么办呢？就是四个字：实事求是。<br />
<br />
　　工作好不好，行动该不该，就看群众需要不需要，喜欢不喜欢。一定要和人民群众心连心，忧其所忧，想其所想，心心相印<br />
<br />
　　薄熙来动情地说，我们是社会主义国家，社会主义国家最本质的属性就是人民当家作主，千千万万的先烈流血牺牲就是为了换来一个人民当家作主的国家。作为执政党，所有的事情都要从人民的利益出发。我们的工作好不好，行动该不该，就看人民群众需要不需要，喜欢不喜欢。群众从内心欢迎的、需要的事，我们就义无反顾，责无旁贷。党的十七届四中全会专门研究党建，而党建最根本的是思想建设，是政治建设。江泽民同志曾大力提倡“三讲”，即讲学习、讲政治、讲正气。我们的法官、公诉人、人民警察，都要讲正气，讲为人民服务。胡锦涛同志特别强调要以人为本，就是要以人民群众的喜怒哀乐为本，凡事都要看人民群众满意不满意，高兴不高兴。我们一定要和人民群众心连心，忧其所忧，想其所想，和他们心心相印，坚持为人民服务的理念。只要如此，党就永远立于不败之地。<br />
<br />
　　不要辜负党多年的培养和人民的热切期望。有多少人正在法院门口眼巴巴的看着、等着我们的正义审判呐！<br />
<br />
　　薄熙来与公检法的干部谈心，人生如梦，日子过得很快，转眼就是60岁。人一生工作的旺盛期也就30年、40年，而大的挑战并不多。当你面对挑战之时，一定要做出正确的选择，并以高度的责任心、事业心为人民群众办好事，办实事。这样，当你60岁退休的时候回过头来看，才不虚度此生。我们年轻的时候都读过奥斯特洛夫斯基那段话，“人最宝贵的是生命，这生命属于人只有一次。人的一生应当这样度过：当他回首往事的时候，不因虚度年华而痛悔，不因碌碌无为而羞愧……”有的人遇到矛盾就躲着走，就烦，就担心；有的人性格就好，越是艰险越向前，还渴望挑战。同志们年富力强，正是承受考验迎接挑战的黄金年华，希望大家不辜负党多年的培养和人民的热切期望。想一想，有多少人正在法院门口眼巴巴的看着、等着我们的正义审判呐！也不要辜负了自己多年的所学，能够站在庄严的法庭来审判罪犯，主持正义，这是多么难得的机会啊！希望同志们能为人民大众做出你们问心无愧的贡献。(重庆日报)(潘 勇 张雪峰)</div>

							
							
							
							
