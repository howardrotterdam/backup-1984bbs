																											<div id="postmessage_66192" class="t_msgfont">副局长因涉嫖娼被拘留10天状告警方<br />
<br />
人民网4月1日报道 昨天上午9点，上城区法院开庭审理一起嫖娼争议案。<br />
<br />
原告：浙江省缙云县××局S副局长，1965年生，公务员。<br />
<br />
被告：杭州市公安局上城区分局。<br />
<br />
2009年1月14日晚上，S副局长在杭州中山国际大酒店桑拿房里，被执行任务的湖滨派出所民警带走。警方认定S副局长嫖娼，对其处以行政拘留十日的处罚。<br />
<br />
从看守所出来后，S副局长将公安局告上法庭。<br />
<br />
焦点：<br />
<br />
有没有发生性行为？<br />
<br />
起诉书说：原告在桑拿后只做普通按摩，没有与服务员发生性行为……派出所民警在检查时只有一人，违反了执行搜查、检查公务时应该有两名以上干警同时在场的规定……原告认为，这个处罚无论是在实体上还是在程序上都是错误的……请求法院判决被告撤消行政处罚决定书，并赔礼道歉。<br />
<br />
S副局长和他的律师坐在原告席上。<br />
<br />
被告席上坐着两名身着警服的民警，一个是上城区公安分局法制科干事，一个是湖滨派出所教导员。<br />
<br />
案子争论的焦点有三点：<br />
<br />
1.有没有发生性行为？<br />
<br />
2.有没有谈及嫖资？<br />
<br />
3.在派出所给S副局长做笔录的，是几个民警？<br />
<br />
警方代理人向法庭申请，让证人出庭。<br />
<br />
主审法官和副审法官商量了一下，同意。<br />
<br />
5位证人依次走上证人席。<br />
<br />
5人全部是民警，其中3人当时参加了清查任务，2人是当时给S副局长做笔录的。<br />
<br />
证人证言<br />
<br />
再现当时一幕<br />
<br />
2009年1月14日晚，湖滨派出所3位民警例行检查中山国际大酒店桑拿中心。<br />
<br />
3位民警两男一女，检查前做了简单分工，女警去控制总服务台，防止服务员通风报信，2位男警，一人先去停车，一人上楼逐间检查包厢。<br />
<br />
负责检查包厢的民警姓周，25岁。<br />
<br />
他说，当他打开3号包厢门时，看到一男一女全裸，正在发生性行为，他当即喝止，并电话通知另外两个同事。看到民警出现，全裸的男女迅速分开，神色慌张，找衣服遮蔽身体。<br />
<br />
两个同事很快赶到，看到一对男女蹲在地上，男的只穿一条短裤，女的裹了一条浴巾。<br />
<br />
穿短裤的男人就是S副局长……<br />
<br />
听到这里，S副局长指着周警官大声喊：“根本没有三个警察，从头到尾我只见到他一个，我没有发生性行为，他一个人看到的，不是真的……”<br />
<br />
“还没到你发言。”法官打断S副局长。<br />
<br />
认定嫖娼的证据有哪些？<br />
<br />
律师问周警官：“他们（指S副局长和女子）当时是什么姿势？”<br />
<br />
“男上女下，全身赤裸。”<br />
<br />
“这个场景几个人看到？”<br />
<br />
“就我一个人。他们分开穿衣服。我的同事很快赶到了。”<br />
<br />
“现场有没有查到避孕套？”<br />
<br />
“没有。”<br />
<br />
律师转头对法官说，抓现行的只有一个民警，没有找到避孕套，不能证明他们正在发生性关系。<br />
<br />
警方代理人拿出当事女子的笔录。笔录说：“没钱才这么做的，听说卖淫的话，客人会给500元，自己能拿200元……民警出现时很慌乱，避孕套不知道藏哪里了……”<br />
<br />
律师话锋一转：“即使发生了性行为，但没有支付嫖资，也不算嫖娼。”<br />
