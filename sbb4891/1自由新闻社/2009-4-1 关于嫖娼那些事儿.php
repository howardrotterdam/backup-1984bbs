警方代理人出示S副局长的笔录，上面有他的亲笔签名和手印。<br />
<br />
笔录中有这样一句话：“……没有谈价格，我想做完到总台结账就行了。”<br />
<br />
警方代理人说：“这说明他们心照不宣，没来得及给钱就被民警抓住，并不妨碍成立卖淫嫖娼行为。”<br />
