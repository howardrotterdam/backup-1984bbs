													<h2>11月2日北京守望教会发布《告会众书》</h2>
						
						
																											<div id="postmessage_320032" class="t_msgfont">深受中共逼迫的北京守望教会，在11月1日不得不到风雪交加的户外敬拜，这显示了信仰的力量，也显示了中共的野蛮、强暴和近似兽性的冷血。北京守望教会本着基督的大爱，在11月2日发布《告会众书》，表明坚持信仰的决心和毅力、表明依靠神必然获胜。以下是该告会众书:<br />
<br />
北京守望教会告会众书<br />
2009-11-02&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
一、教会的现状<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;守望教会于2009年10月30日搬出华杰大厦，至此教会告别了三年半在华杰熟悉温暖的聚会场所。相信多数弟兄姊妹是带着感恩的心离开华杰的。在这里神使他的守望教会有勇气进入到社会公共领域公开聚会；在这里神使我们告别了团契时期而成为一个整合的教会；在这里神使我们教会的整体事工上到一个新的台阶。教会在华杰的成长是每一位弟兄姊妹都深切经历到的，其中也包含着我们每个人与教会的一同成长。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;守望教会从华杰搬出，主要是由于租房给教会的房东们自今年8月起就持续地受到北京海淀区有关部门从工商税务等各方面对他们的工作及生活所施加的压力。出于基督的爱，教会不愿意让房东承担本应由教会承担的压力，我们决定接受房东应有关部门要求提出的提前解除租房合同的请求，承诺于10月30日从华杰所租用的房屋中搬出。教会对于一个本应以“为人民服务”为其宗旨的相关政府部门，对于广大守法的基督徒公民至今没有表现出应有的善意感到遗憾。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;在此之前，教会在恒润等写字楼租房因受到同样压力而离开，特别是去年奥运会前，我们在华杰遭遇冲击，有关部门对房东们施压时，教会认识到，房东替我们承受压力已经成为教会发展的“软肋”。随着教会规模的自然成长，教会已经不再能够像团契时期那样可以随时地从一处搬迁到另一处。因此去年的冲击事件后，教会就成立了“建堂筹备小组”，寻求以购买的方式解决聚会场所问题。在筹备小组长达几十页报告的基础上，教会治理委员会最终形成了“09建堂方案”。这个方案经过同工会及教会代表大会印证通过后，教会建堂于今年三月底正式启动。从建堂启动到如今，教会经历到前所未有的同心合一，大家相信神施恩的手一直在带领着他的教会。不过，两次交易谈判，在谈判小组经教会授权让步到答应了业主的报价要求后，在我们在所定的期限内，我们再没有听到对方的回音。个中原因，我们目前还不得而知。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;出于上述原因，教会迫不得已选择了户外聚会。我们承认我们没有预备好要到户外聚会。但直到最后一刻，我们一直经历着神赐给我们的平安，这平安让我们确信教会一直在我们主的掌管之中。神的意念高过我们的意念，神的道路高过我们的道路。在人看来不得已的户外聚会，相信在神看来是于他的教会有益。建堂不只是购置一个场所，更是建造教会和弟兄姊妹的生命。我们欣慰地看到，弟兄姊妹同怀一样顺服的心，于11月1日的主日风雪无阻地来到海淀公园东门参加主日崇拜。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;由于公园东门在一纸通告后面大门紧闭，教会只能在东门外的宽阔的空地中举行崇拜。二十多年未见如此之早临到的大雪给守望教会在户外的聚会平添了几分让人惊奇的色彩。上午10点钟崇拜在诗班庄严的歌声中正式开始，音乐敬拜、讲道、祷告、家事分享，这些程序与在华杰中的崇拜过程一样。天明牧师的讲道比平常略短，他用希伯来书的11章23到31节，神带领摩西和以色列人出埃及是借着在他们生命中培育信心并在信心中作出抉择，来激励弟兄姊妹；同时也解释了教会目前建堂的处境，鼓励大家能够与教会一同跟随主的脚步。本主日三堂合为一堂，参加聚会的人数约为500人，其中包括几位七十多岁的年老弟兄姊妹。大家心里火热，祷告有力。11点多崇拜结束时大家相互问候，十分喜乐。<br />
<br />
二、我们的盼望与祷告<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;教会处在这个特别的时期，请弟兄姊妹为如下几个方面恳切地向我们的神祷告。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;首先，既然神定意要他的守望教会进入到旷野之中，我们盼望神在这一段时间特别保守教会的聚会，“白日用云柱，夜间用火柱”。我们不知道我们会在户外聚会几次，我们只有祷告，求神让他的百姓能够有力量面对北京冬天零下十几度的天气。我们盼望神能够保守教会每一位他的儿女能够与教会同行，不发怨言，生命与教会一同得到历练。我们相信，尽管我们不配，但神定意要让我们所经历的这一切都是为了造就和使用他的教会。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;其次，我们盼望弟兄姊妹也能够恳切地祷告，求神让我们时常警醒，不致中了撒但的诡计。这诡计之一就是要让教会在户外的临时聚会被渲染上一层“政治”的色彩。教会在户外聚会是不得已的。在一时没有聚会场所，分散聚会又违背神对守望教会的带领，按圣经的教导我们又不可停止主日崇拜的情况下，我们只能谦卑地求神保守我们暂时在户外的崇拜。我们不是为了抗议而走到户外的。这是我们在神的面前所存的良心。我们求神给我们智慧，让我们的聚会不致因为某种“政治”理由而受到影响。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;第三，我们盼望教会在户外的聚会是临时的。我们恳求神怜悯他的守望教会，让教会能够早日进入他应许要赐的迦南美地。求神继续带领教会的建堂进程，帮助我们能够早日进入新堂。由于目前北京楼市的高价格，我们还需要一千万的资金。在这个过程中，我们还要继续地仰望神的供应，并恳求神在这个时期保守弟兄姊妹的奉献。我们相信神要将他的教会带到更美的地方，为此，求神让我们大家一同仰望等候，心存耐心与感恩。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;最后，让我们也为这一段时间众教会一直为守望教会代祷、在建堂过程中的多方支持表示感恩。愿神记念他们在基督里同心的祷告与支持，并盼望众教会在这段时期能够继续为我们代祷。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;我们不知道前面教会会遇到什么，这是一条我们这代人没有走过的路。但我们相信，我们没有走过的路，主都走过。只要我们愿意将自己摆上，愿意付出任何的代价，甘心跟随他的脚步，主一定会带领我们在他的里面经历得胜，并与他一同进入迦南美地。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;愿神保守他的守望教会！阿们！<br />
<br />
北京守望教会<br />
2009年11月2日</div>

							
							
							
							
