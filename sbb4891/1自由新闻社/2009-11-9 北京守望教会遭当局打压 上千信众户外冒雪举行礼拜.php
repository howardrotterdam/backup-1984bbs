													<h2>北京守望教会遭当局打压 上千信众户外冒雪举行礼拜</h2>
						
						
																											<div id="postmessage_320024" class="t_msgfont">　　在多方压力下，守望教会发布公告：北京守望教会定于2009年10月30日从华杰搬出，自11月1日的主日起不再在华杰原有的主副堂进行主日崇拜，请没有接到教会通知的弟兄姊妹相互转告。<br />
&nbsp; &nbsp; 守望教会网站已经于4月被政府关闭，守望教会因为无法寻找到新的崇拜地点，11月1日在海淀公园外举行露天崇拜，随后事态进一步恶化，北京守望教会已经成为百度的敏感词。豆瓣的守望教会小组也已被解散。<br />
&nbsp; &nbsp; 此前守望教会曾寻求向政府登记，不过因为其拒绝承认政府认定的“三自爱国教会”权威，其审批未能通过。关于此次户外崇拜，负责人曾表示，户外崇拜是迫不得已，并非抗议。<br />
<br />
　　VOA报道：<br />
<br />
　　北京守望家庭基督教会因当局向房东施压，无法继续租用原来的聚会场所，迫使近800名教友星期天在北京降下的第一场冬雪中，在北京的海淀公园东门外举行露天主日崇拜。此外，中国社会科学院法学研究所副研究员范亚峰，也由于参与基督教维权工作而被开除。关注中国宗教自由的团体说，中国家庭教会受到当局打压的情势越来越为严峻。<br />
<br />
　　*守望教会租房解约被迫在雪中做露天崇拜*<br />
<br />
　　设在美国的宗教组织《对华援助协会》说，北京守望教会原先租用的华杰大厦从8月以来就受到税务当局的骚扰和海淀区国保大队的压力，迫使他们解除和教会的合同。守望教会牧师金天明因不愿造成房东困扰，同意解除租房合同，在上星期五搬出了租用3年半的聚会所。<br />
<br />
　　由于一时找不到其他场所，星期天教会只好临时通知教友在海淀公园的东门外空地上聚会，而北京此时也正好降下了今年秋冬以来的第一场大雪，将近800人在寒冷的气候中唱诗、祷告，进行主日崇拜。<br />
<br />
　　*基督教维权律师范亚峰被社科院解聘*<br />
<br />
　　与此同时，经常为家庭教会发声的基督教维权律师范亚峰，星期一也接到工作单位中国社科院不再续聘的通知。范亚峰对美国之音说，他的下岗和他经常呼吁进行政治改革有关。<br />
<br />
　　他说：“理由一以贯之，就是我参与了一些维权活动，另外就是呼吁一些政治改革、政治变革方面一些立场观点和他们不是很一致。”<br />
<br />
　　范亚峰说，对于失去工作，他在多年前投身维权事务时就已有心理准备，而他的目标和理想，也不会因此有所改变。<br />
<br />
　　*范亚峰：坚持继续进行维权工作*<br />
<br />
　　他说：“很多年之前，自己投身法学研究和推动法治的维权行动时就已经在学习做好准备，随时可能丢掉工作，甚至是进监狱。所以今天这个事情出现，我倒是很坦然，而且应该说是很感恩。就我自己来讲，后面还会继续的坚持自己的研究和维权行动，不会因为这个事情有太大的改变。”<br />
<br />
　　对于包括守望教会在内的一些家庭教会受到中国当局的打压，范亚峰说，中国的宗教和基本人权的保障，现在正面临关键时刻，大家必需给予极大的关注。<br />
<br />
　　北京守望教会海外成员强烈呼吁敬拜权利<br />
<br />
　　来美国的北京守望教会信徒穆文斌日前在美国呼吁中国政府应给予守望教会信仰自由及敬拜自由，并呼吁全世界信徒关注此一事件，即将访中的奥巴马总统也应该关注此事。他并呼吁中共应给予中国上亿名基督徒信仰基本权利：聚会敬拜和自建教堂。以下是他的全文。<br />
<br />
　　2009年11月1日 穆文斌 美国<br />
<br />
　　本人是一名来自北京守望教会的基督徒，现在就读于美国某神学院。北京守望教会是北京乃至中国最大的家庭教会之一，由金天明牧师于1993年在北京中关村地区创建，目前每周参与聚会敬拜的信徒有近千人，本人曾受洗于北京守望教会。<br />
<br />
　　11月1日，我在美国得知来自北京的最新消息，中国国内家庭教会形势严峻，继山西临汾教会由于教产问题受到全面逼迫之后，本人曾经聚会的北京守望教会聚会场所由于公安施压，租期到10月29日期满，无法续租，也找不到新的聚会场所，购买办公楼事宜没有签约，（北京时间）10月31日（周日）被迫到北京海淀公园东门公园聚会。据当天守望教会参加聚会的弟兄介绍, 尽管当天北京下着今冬首场大雪, 还是有大约800到1000人冒雪坚持举行了教会户外主日聚会敬拜。<br />
<br />
　　本人到今年9月底一直在守望教会位于北京大钟寺华杰大厦八层的聚会点进行主日聚会敬拜，但是自8月上旬以来，海淀区民宗侨及国保大队等有关部门不断向北京守望教会租房房东北京华杰大厦施加压力，逼迫房东与教会解除合同。<br />
<br />
　　为此，教会8月19日金天明牧师等4人代表教会赴海淀区民宗侨及国保大队提出质询；20、26日教会举行特别祷告会；23日主日证道，金天明牧师申明教会立场：倘若可行，求神保守教会在华杰平安聚会到合同期满；若是不能，又暂时不能进入新堂，教会只能在户外坚持聚会。教会在10月30日发出通知如下：“教会通知：北京守望教会定于2009年10月30日从华杰搬出，自11月1日的主日起不再在华杰原有的主副堂进行主日崇拜，请没有接到教会通知的弟兄姊妹，与所熟知的弟兄姊妹联系以确认新的聚会的地方。”<br />
<br />
　　自去年奥运会召开前，5月11日海淀区民宗侨及国保大队等有关部门就闯进北京守望教会主日敬拜主堂和副堂，守望教会天明牧师无视干扰，坚持继续进行聚会敬拜。十六年以来，北京守望教会就是在这种逼迫中发展成长起来，一直到现在近千人的规模。<br />
<br />
　　北京市有关政府部门希望北京守望教会加入中国基督教三自爱国运动委员会和中国基督教协会，8月23日金天明牧师在证道结束后再次重申了北京守望教会不能加入“三自教会”的原因：<br />
<br />
　　1、“三自教会”是政治运动的产物，守望教会反对教会参与政治运动，持守“政教分离”原则。<br />
<br />
　　2、“三自教会”没给中国基督教会带来祝福，北京解放前有60多家基督教会，现在只有4-5家基督教会，很多牧师殉道，基督教会受到严重逼迫，教会受到重大损失。<br />
<br />
　　3、“三自教会”主张自由派观点，减弱了耶稣基督的神性、圣经的权威和因信称义的真理，信仰上不能变通。<br />
<br />
　　4、参加“三自教会”与教会自立、自传、自养、自治的原则不同（王明道牧师语），守望教会以“三自”的方式实现了“三自”，不能参加“三自教会”组织。<br />
<br />
　　5、参加“三自教会”与爱国无关。<br />
<br />
　　2004年中国国务院颁布了《宗教事务条例》，由于参加“三自教会”行不通，北京守望教会自2005年开始推动登记教会，2006年6月向国家民政部、宗教局及北京民政局、宗教局申请教会注册登记，民政部门以守望教会“没有牧师”为由，必须有“三自教会”认可的“牧师”才能登记注册。2007年7月守望教会到国家宗教局申请教会登记，至今没有任何答复。<br />
<br />
