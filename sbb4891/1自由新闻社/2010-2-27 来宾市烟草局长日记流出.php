十二月<br />
　　<br />
　　12月1日　　　星期六　　　　　　　　　　　　　　　　　　　　11－22℃晴<br />
　　<br />
　　上午和阿爸走去文化市场，买点东西。晚上去吃饺子。<br />
　　<br />
　　12月2日　　　星期日　　　　　　　　　　　　　　　　　　　14DD23℃阴<br />
　　<br />
　　一天在家。<br />
　　<br />
　　12月3日　　　星期一　　　　　　　　　　　　　　　　　　　8DD23℃晴<br />
　　<br />
　　上午阿武来了后回来宾。下午去办公室处理一下文件，结果综合科的一帮人叫和他们吃饭，就去了喝多了。<br />
　　<br />
　　12月4日　　　星期二　　　　　　　　　　　　　　　　　　　7DD23℃晴<br />
　　<br />
　　上午休息，中午时钦州法院的赵锌到这里的法院办事，叫出去吃饭，和小盘去了，一喝就喝到4点多，喝多了。小盘也多了，叫她到我房间，和她****，记得好像很激烈，她也配合动了，好像水好多。晚上小盘和阿梅叫出去吃霄夜，一吃又叫覃刚来又喝了两瓶洋酒，又多了。<br />
　　<br />
　　12月5日　　　星期三　　　　　　　　　　　　　　　　　　　13DD22℃多云<br />
　　<br />
　　昨天和小盘****厉害，全身都酸痛。一天在宿舍。<br />
　　<br />
　　12月6日　　　星期四　　　　　　　　　　　　　　　　　　　12DD20℃阴<br />
　　<br />
　　本来上午要开中层干部述职会的，结果李副市长叫我过去汇报，就让他们先开着，结果和李只聊了半个小时，先回宿舍休息，10点多才去参加会议。下午党组民主生活会，区局和市里也还是没有人来参加，哈！5年了他们都不来多好的。晚上是王全来，和他们喝了不少酒，但今晚没事。<br />
　　<br />
　　12月7日　　　星期五　　　　　　　　　　　　　　　　　　　8DD21℃多云<br />
　　<br />
　　上午10点和他们开一下会研究一下明年的订货问题。下午2点半和阿武回南宁，晚上大家去“竹蓠芭”吃饭，明天请阿武送阿爸回梧州了。<br />
　　<br />
　　12月8日　　　星期六　　　　　　　　　　　　　　　　　　　13DD21℃阴<br />
　　<br />
　　上午9点让阿武送阿爸回梧州。结果他们在路上被堵了2个小时。和莎去帮儿子买游戏碟，再去老梦吃饭回。<br />
　　<br />
　　12月9日　　　星期日　　　　　　　　　　　　　　　　　　　14DD23℃阴<br />
　　<br />
　　上午和莎去韦萤处买了个漫步者H260耳塞，花了160元，真好！<br />
　　<br />
　　12月10日　　　星期一　　　　　　　　　　　　　　　　　　16DD23℃多云<br />
　　<br />
　　上午在家。下午阿武来了后再回来宾。晚饭和安徽的老高和江苏的王处一起吃饭，喝了不少酒。晚上机关的演出，给他们照相。<br />
　　<br />
　　12月11日　　　星期二　　　　　　　　　　　　　　　　　　17DD26℃阴<br />
　　<br />
　　一天经济运行分析会。晚上和黄桂廷及土地局的肖局等一起吃饭，定了我们出报名费500万，以确保地皮到我们手，又喝了不少。<br />
　　<br />
　　12月12日　　　星期三　　　　　　　　　　　　　　　　　　15DD22℃多云<br />
　　<br />
　　上午去办公室和来宾信用社的汤主任谈一下，他们把电结的费用率降下来了，由千分之三降到千分之一，但要给他们留余额。下午和李金友和黄丹明去南宁参加区局的用工和分配改革会议报到，明天开一天。<br />
　　<br />
　　12月13日　　　星期四　　　　　　　　　　　　　　　　　　15DD19℃阴<br />
　　<br />
　　一天会议。传达、领导讲话、讨论、总结。张长胜和阿贵参加会议了。晚上和张长胜、阿贵他们一起吃饭。明天不回去了。<br />
　　<br />
　　12月14日　　　星期五　　　　　　　　　　　　　　　　　　14DD20℃阴<br />
　　<br />
　　一天在家。中午和莎出去吃粉。<br />
　　<br />
　　12月15日　　　星期六　　　　　　　　　　　　　　　　　　16DD22℃阴<br />
　　<br />
　　上午和莎去新梦走走，人多，梦之岛正在搞活动。吃了饭回。<br />
　　<br />
　　12月16日　　　星期日　　　　　　　　　　　　　　　　　　15DD23℃阴<br />
　　<br />
　　一天在家，用Photoshop处理黑白照片，掌握了。明天张天杰来人去来宾搞班子年底考核，8点叫阿武来回来宾。<br />
　　<br />
　　12月17日　　　星期一　　　　　　　　　　　　　　　　　　14DD21℃阴<br />
　　<br />
　　上午10点半他们到了后开会我们述职。下午他们找人谈话，我给中层干部传达用工分配会议精神。晚上走了几摊，喝了不少酒。梁四过来了，晚上还和他们法院的去唱了下歌。<br />
　　<br />
　　12月18日　　　星期二　　　　　　　　　　　　　　　　　　10DD12雨<br />
　　<br />
　　冷了下雨。上午他们还是谈话。我给李金友交代一下工作。明天要去象州搞中心组学习，再准备一下材料，要给他们做一下十七大辅导。下午他们谈完了班子成员后给班子反馈情况后他们回去了。<br />
　　<br />
　　12月19日　　　星期三　　　　　　　　　　　　　　　　　　10DD13℃雨<br />
　　<br />
　　一早去象州，在马坪乡的盘古天地搞中心级学习。10点开始，先让来宾讲师团的讲，我下午给他们再讲一下十七大精神。晚上和他们喝了不少酒<br />
　　<br />
　　12月20日　　　星期四　　　　　　　　　　　　　　　　　　12DD20℃雨<br />
　　<br />
　　上午讨论。下午我给三个县员工讲文明礼貌。晚上7点汇演，大家高兴，但水平不高，今后要提高水平。和他们一起吃消夜，喝了不少酒。<br />
　　<br />
　　12月21日　　　星期五　　　　　　　　　　　　　　　　　　11DD14℃雨<br />
　　<br />
　　吃了早餐回来宾，到办公室处理一下文件。今天地皮公示了，让他们去办。下午和阿武回南宁。来宾一直下雨，又冷才12℃，到了南宁却是26℃，到了半夜才冷点。<br />
　　<br />
　　12月22日　　　星期六　　　　　　　　　　　　　　　　　　14DD17℃雨<br />
　　<br />
　　上午和莎上街去一下新梦，吃了饭回。下午搞一下歼十飞机的垂尾，买回来时没有注意少了垂尾，用塑料板做了一个，要买回涂料涂好。<br />
　　<br />
　　12月23日　　　星期日　　　　　　　　　　　　　　　　　　12DD17℃阴<br />
　　<br />
　　一天在家。莎出去帮仔买书，顺帮我把飞机模型的颜料买回来，中午把飞机的垂尾搞好。<br />
　　<br />
　　12月24日　　　星期一　　　　　　　　　　　　　　　　　　9DD18℃多云<br />
　　<br />
　　上午和莎去图书批发市场，在书店买到了RAW的书，太好了！下午在区局参加明年烟草税务全国大检查的布置会。晚上和他们一起吃饭后回来宾。<br />
　　<br />
　　12月25日　　　星期二　　　　　　　　　　　　　　　　　　9DD18℃阴<br />
　　<br />
　　上午在宿舍。下午大学生座谈会，晚上和大家喝了不少酒。<br />
　　<br />
　　12月26日　　　星期三　　　　　　　　　　　　　　　　　　12DD17℃阴<br />
　　<br />
　　上午在宿舍。下午去办公室处理一下文件，今天访销完了，约超了100多箱，今年又是全面完成任务。今天要物流中心地皮的报名搞完了，交了500万元。晚上和黄桂廷等一起吃饭，喝了不少酒。<br />
　　<br />
　　12月27日　　　星期四　　　　　　　　　　　　　　　　　　12DD17℃小雨<br />
　　<br />
　　上午是营销中心客户经理技术大比拼。下午准备一下明天的讲话，5点半过去给他们颁奖。晚上先客户经理吃饭，再去一下人防办吃饭的地方，再和李军他们一起喝啤酒，又喝了不少。<br />
　　<br />
　　12月28日　　　星期五　　　　　　　　　　　　　　　　　　11DD14℃小雨<br />
　　<br />
　　上午客户经理技术比拼总结，我给他们讲了2人小时的话。中午开了个房叫阿芳过来****，做了很多次，累坏了。4点去一下办公室。晚上和专卖办的人一起吃饭，又喝了不少的红酒，12点过国大再和阿芳****，又做了很多次。<br />
　　<br />
　　12月29日　　　星期六　　　　　　　　　　　　　　　　　　9DD12℃多云<br />
　　<br />
　　早上又和小谭做了一次，射了。10点回，去一下办公室。下午一点和阿武回南宁，接了莎去钦州，晚上一家人吃饭。<br />
　　<br />
　　12月30日　　　星期日　　　　　　　　　　　　　　　　　　8DD18℃多云<br />
　　<br />
　　上午和莎梦之岛水晶城，正在搞活动人多。我买了双法骆的皮革和一条皮带。吃了饭回。下午在家。<br />
　　<br />
　　12月31日　　　星期一　　　　　　　　　　　　　　　　　　9DD16℃多云<br />
　　<br />
　　上午和莎去新梦，这里也在搞活动我更多。买了两个电暖手毯，吃了饭回。下午在家。<br />
　　<br />
　　二七年过去了，这一年我的工作更顺手了，企业规范，中层干部努力而理解我的工作意图，我在员工中的威信高，今年更是全面完成任务，收入又高达 20万元，明年更好做。鉴于此我都无所谓回不回区局了，能再干二年直接回区局干个闲职再内退最好。今年儿子又争气，得到了推荐研究生，不用考了，两年后他找工作会容易多。今年自己玩相机上了一个台阶，今后就会好好地玩到老。玩女人上，终于玩了，又和小盘玩上了，还固定地和谭善芳玩，还有个莫瑶待玩，今年真是交了桃花运，女人多了就是要注意身体。</div>

							
							
							
							
