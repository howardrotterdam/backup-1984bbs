<br />
早上早起，坐景区车去坐缆车上金顶，缆车较陡，上去要25分钟。上去再走约100米上到顶。这里的景好，可惜没有遇到云雾，倒是在半路上遇到了，但没停车。看完下山时开始下雨了，好。下山中停了一下车，拍到雾景了。下了山吃了中饭赶回武汉。6点半到，吃了饭8点半了，仍住亚酒<br />
