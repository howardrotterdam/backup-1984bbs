													<h2>广西来宾市烟草局局长韩峰的日记（日记门事件）</h2>
						
						
																											<div id="postmessage_495947" class="t_msgfont">9月14日　　　星期五　　　　　　　　　　　　　　　　　　　24DD31℃雨<br />
上午和莎去民族大道的电信网点，她要退了小灵通，电信真讨厌！骗人开户就到处都有点给你开户，但退户时却只有一个指定的点，工作人员还才一个，排队5个人，却排了二个小时才办完，真不怪社会非议他们！吃了饭回。<br />
9月15日　　　星期六　　　　　　　　　　　　　　　　　　　25DD30℃晴<br />
上午和莎去五象广场走走，吃了饭回。<br />
9月16日　　　星期日　　　　　　　　　　　　　　　　　　　24DD31℃晴<br />
上午莎去买菜。中午王书城叫去桂景大酒店吃饭，就我们俩，他给了两瓶茅台酒和5万元，存了3万，拿两万回去。<br />
9月17日　　　星期一　　　　　　　　　　　　　　　　　　　23DD33℃晴<br />
上午阿武到了后回来宾。中午何庆清等一帮人过来，一起喝了不少的酒，他们下午走了。<br />
9月18日　　　星期二　　　　　　　　　　　　　　　　　　　21DD32℃晴<br />
上午在宿舍。下午先去国大要了个房再去办公室，容日谦等来，明天他们考专卖员，晚上和他们一起喝了不少的红酒。11点多去国大，小谭早到了，她来月经，用嘴和我做。<br />
9月19日　　　星期三　　　　　　　　　　　　　　　　　　　21DD32℃晴<br />
上午在宿舍，中午红河、安徽、李月芬等来，和他们一起吃饭，喝了不少酒。下午睡了一觉，晚上去国大，小谭还是用嘴和我做，射了。<br />
9月20日　　　星期四　　　　　　　　　　　　　　　　　　　19DD32℃晴<br />
一早小谭走了我也回了，累。下午到办公室，陈璋到办公室坐一下，给了1万元，李给了2000元。<br />
9月21日　　　星期五　　　　　　　　　　　　　　　　　　　20DD30℃晴<br />
早上去办公室处理一上文件。下星期二是八月十五，不回来宾了，星期六才回。下午和阿武回南宁，刘林林买了个HTCS1让我给他装一下软件。晚上给他装好。<br />
9月22日　　　星期六　　　　　　　　　　　　　　　　　　　23DD32℃多云<br />
上午等刘林林过来拿了手机后和莎去韦萤处买了个电脑游戏给霜。吃了饭回。<br />
9月23日　　　星期日　　　　　　　　　　　　　　　　　　　23DD31℃多云<br />
一天在家。中午唐仁军请莎等出去吃饭。<br />
9月24日　　　星期一　　　　　　　　　　　　　　　　　　　23DD30℃台风雨<br />
台风影响，下雨又冷。一天在家。邓又拿了一堆的海鲜来。明天就是八月十五了，星期三又要报到开会，不去来宾了。<br />
9月25日　　　星期二　　　　　　　　　　　　　　　　　　　21DD26℃雨<br />
霜叫寄索尼T20数码相机（常德中烟给）给他，上午和莎先去韦萤处要了一个1G的记忆捧后去寄给他，邮费60元。下午在家。<br />
9月26日　　　星期三　　　　　　　　　　　　　　　　　　21DD30℃多云<br />
上午阿武到后去买了个LGKW820双模双待手机，花了3720元。回来宾。下午到办公室，处理一下文件和他们谈一下销售的事。晚上小谭要和我做，她29号要结婚了还要和我玩，看来这个女孩真是太野了！到国大开了个房，她10点多到，洗一下，上床就做，动作很大。一起聊天，半夜又做一次，她又出血了。睡了一下，早上又做一次，这次没有出血。<br />
9月27日　　　星期四　　　　　　　　　　　　　　　　　　24DD32℃小雨<br />
早上9点半回房间，再续了一天房给小谭。一天在宿舍。<br />
9月28日　　　星期五　　　　　　　　　　　　　　　　　　25DD34℃多云<br />
上午在宿舍。下午到办公室处理一下文件。晚上老段来，和他们喝了不少酒。<br />
9月29日　　　星期六　　　　　　　　　　　　　　　　　　23DD32℃多云<br />
上午在宿舍。下午到办公室。晚上和一帮中层陪交警大队的林政委吃饭，又喝了不少酒<br />
9月30日　　　星期日　　　　　　　　　　　　　　　　　　24DD33℃晴<br />
上午9点和阿武回南宁。<br />
十月<br />
10月1目　　　星期一　　　　　　　　　　　　　　　　　　24DD32℃多云<br />
一天在家。<br />
10月2日　　　星期二　　　　　　　　　　　　　　　　　　23DD28℃阵雨<br />
一天在家。台风影响有点变凉。<br />
10月3日　　　星期三　　　　　　　　　　　　　　　　　　23DD27℃阵雨<br />
上午和莎先去电科广场卖了张TF1G卡，我S1用的，原来的给了小谭。再去老梦吃饭。这两天在家玩手机。<br />
10月4日　　　星期四　　　　　　　　　　　　　　　　　　24DD30℃雨<br />
上午在家玩手机。下午和莎上街，先去文化市场买书，再去万达广场走走，买了快餐回家吃。<br />
10月5日　　　星期五　　　　　　　　　　　　　　　　　　24DD31℃阴<br />
一天在家玩手机。<br />
10月6日　　　星期六　　　　　　　　　　　　　　　　　　24DD34℃多云<br />
一天在家玩玩手机<br />
10月7日　　　星期日　　　　　　　　　　　　　　　　　　22DD33℃阴<br />
一天在家看看书和F1。<br />
10月8日　　　星期一　　　　　　　　　　　　　　　　　　21DD32℃多云<br />
上午阿武到后回来宾。下午去一下办公室，处理文件。晚上是冯太生和老马的生日，大家一起吃饭，喝了不少酒，阿刚和老马醉得厉害。<br />
10月9日　　　星期二　　　　　　　　　　　　　　　　　　23DD31℃多云<br />
上午0点到办公室和综合科的相关人员邢个会讲讲信息整合的工作。下午开党组会。<br />
10月10日　　　星期三　　　　　　　　　　　　　　　　　　20DD30℃晴<br />
上午在宿舍。下午去办公室处理一下文件，到国大开了个房，晚上红河欧阳来，我不陪他吃饭了，免得吓了人。11点去国大和小谭****，做了三次，射了，累。<br />
10月11日　　　星期四　　　　　　　　　　　　　　　　　　20DD30℃晴<br />
上午红河欧阳过来，和他聊了一下，明年红河满足我们6千箱的供货。晚上廖平来，一起吃饭，黄贵廷也参加。<br />
10月12日　　　星期五　　　　　　　　　　　　　　　　　　21DD30℃阴<br />
上午去一下办公室。下午3点和阿武回南宁。<br />
10月13日　　　星期六　　　　　　　　　　　　　　　　　　21DD30℃多云<br />
上午和莎去韦萤处，霜的电脑不行了，给他找个笔记本电脑，本来联想旭日C460A挺好的，才在6500元，但无货，只好找了惠普DV2621,7500元。他这次通过了学校推荐研究生的面试，公布3天即可，应届毕业600多人只有16人，分A、B、C三类，他是A类。<br />
10月14日　　　星期日　　　　　　　　　　　　　　　　　　21DD28℃多云<br />
上午去理发。下午在家。明天和莎、卢建涛等飞深圳。<br />
10月15日　　　星期一　　　　　　　　　　　　　　　　　　23DD29℃晴<br />
12点半卢建涛和阿武到，一起去机场，腾珊娜小林已经送去机场。3点多到深圳，住下。晚上厂里的何书记、宋厂长、销售部的所有领导都来了，喝了不少洋酒，多了。<br />
10月16日　　　星期二　　　　　　　　　　　　　　　　　　23DD30℃晴<br />
上午吃了早餐，李经理陪我们去看明斯克号航母，在那花950元买了个1：30的歼十金属模型，好！回到宾馆莎等她的朋友们，我们再去买水货手机，真的很便宜，高档机的差价有1000多元，给莎买了个三星U608。晚上又喝酒。<br />
10月17日　　　星期三　　　　　　　　　　　　　　　　　　9DD17℃<br />
也不想去哪了，在宾馆。12点老温和老徐过来接我们，李经理陪何书记去广西了。吃了饭去机场，莎飞回南宁，我们去贵阳，准时起飞。贵阳冷，阿苏和李金友已经过来了，晚上吃饭贵阳厂来了很多人，销售公司的李书记和他们的纪检书记，人力资源部长都来了，都醉了不少人，晚上再去唱歌，结果鲍主任又醉了。我还好没事。<br />
10月18日　　　星期四　　　　　　　　　　　　　　　　　　8DD15℃雨<br />
吃了早餐朱兵陪我们去熄烽集中营，在贵去遵义的一半路上，不远但不好走，又是修路。这里曾是国民党最高等级的监狱，后搬去重庆。看完后吃饭，都2点多了。回到贵阳休息。晚上王苏瑶请我们一起吃饭，又喝了不少酒。<br />
10月19日　　　星期五　　　　　　　　　　　　　　　　　　10DD18℃阴<br />
早上自己吃的早餐，阿苏和李金友等去黄果树，我和冯、梁、卢飞南宁。12点去吃饭，2点35的飞机，准时飞南宁。冯等回，我明天还要回来宾和深圳的何书记等上圣堂山。<br />
10月20日　　　星期六　　　　　　　　　　　　　　　　　　18DD28℃晴<br />
吃了早餐9点和阿武回来宾。12点半深圳厂的何书记等到，陪他们吃饭后去金秀。阿梅和钟向东给他们买石头后到。晚上大家喝杨梅酒，都醉了。<br />
10月21日　　　星期日　　　　　　　　　　　　　　　　　　15DD28℃晴<br />
上午吃了早餐，上山。刚上山时有点虚脱，走了三分之一好了。今天是大晴天，莫瑶来着走了五指山的景，上次没有到，太漂亮了！还是3点半在山上吃饭，4点多点下山，6点20分离开，8点半回来金秀，李金友也过来了，一起喝了点酒，半夜起不来看F1了。<br />
10月22日　　　星期一　　　　　　　　　　　　　　　　　　17DD30℃晴<br />
上午吃了早餐，送他们到鹿寨的高速路口，他们去桂林。我回来宾，看一下工地和物流，过几天国家局来检查。下午在宿舍。F1莱科宁第一，并得了年度冠军，积110分，第二阿隆索积109分，汉密尔顿第三积109分。<br />
10月23日　　　星期二　　　　　　　　　　　　　　　　　　17DD30℃晴<br />
上午到办公室，处理一下文件。下午和绩效组的教师们一起交换意见，晚上一起吃饭，喝了不少。<br />
10月24日　　　星期三　　　　　　　　　　　　　　　　　　19DD31℃晴<br />
上午在宿舍。中午梁德强带了个内管检查组来检查内管工作，和他们一起吃饭。下午四点他们开了个见面会。晚上帮老段安装他的S1手机。<br />
10月25日　　　星期四　　　　　　　　　　　　　　　　　　19DD32℃晴<br />
上午在宿舍，写一下讲座。下午国家局检查组来检查安全，给他们汇报，吃了饭他们去柳州，我即回南宁，明天飞武汉。<br />
10月26日　　　星期五　　　　　　　　　　　　　　　　　　19DD32℃晴<br />
上午10点半去机场。12点半的飞机，准点到。住“帅府”。晚上霜过来。<br />
10月27日　　　星期六　　　　　　　　　　　　　　　　　　18DD28℃阴<br />
上午三人去电脑市场走走，给霜买了个蓝牙器。在群光吃了午饭回。下午小三过来，一起在“大三利”吃饭。<br />
10月28日　　　星期日　　　　　　　　　　　　　　　　　　15DD23℃雨<br />
上午先到霜的住处给他整理一下东西，旧电脑搬好，腾出台面给他。中午想去“太子”吃的，给果到外结婚，只好走到理工大南湖校区边的一个小餐馆吃湖南菜了。饭后走一下南湖校区，初步有形了，很大。下午先去买了两U盘本想去“太子”的的士又不去，是因为交班，只好到咖啡厅吃了。<br />
10月29日　　　星期一　　　　　　　　　　　　　　　　　　13DD20℃雨<br />
上午和莎走走“新世界”商场，走到“太子”酒店点好菜等霜，他12点到，一起吃饭，他回学校。下午和莎想打的去汉口，结果的士都不愿过去，只好做罢，叫他们来接了。6点车来，过去后都7点多了，他们营销公司的马书记陪吃饭。明天早走，叫莎去搬过来住“亚洲大酒店”了。<br />
10月30日　　　星期二　　　　　　　　　　　　　　　　　　9DD13℃雨<br />
早上吃了早餐8点半走，去武当山，走了不到5个小时，到了十堰市的武当开发区，就是武当山的山底。吃了中饭，导游带上山。看了太子坡和南岩天黑了，再下到乌鸦岭住。晚上冷。<br />
10月31　　　　星期三　　　　　　　　　　　　　　　　　　7DD11℃雨<br />
早上早起，坐景区车去坐缆车上金顶，缆车较陡，上去要25分钟。上去再走约100米上到顶。这里的景好，可惜没有遇到云雾，倒是在半路上遇到了，但没停车。看完下山时开始下雨了，好。下山中停了一下车，拍到雾景了。下了山吃了中饭赶回武汉。6点半到，吃了饭8点半了，仍住亚酒</div>

							
							
							
							
