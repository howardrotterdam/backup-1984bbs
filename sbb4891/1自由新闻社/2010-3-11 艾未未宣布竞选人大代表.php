　　答：1、关注竞选者所在县（或区或县级市）或乡或选区大多数选民关注的热点问题、难点问题和焦点问题。在公开场合敢说大多数选民想说而又一时不愿说的真话；不畏权势、不怕打击、不图私利；以实现政治民主、思想自由、文化多元的一个符合人的尊严和社会公正的社会为己任；尽最大可能维护弱势群体或这个群体中的部分人的利益，以及为他们争取到实实在在的经济利益和政治权利。<br />
