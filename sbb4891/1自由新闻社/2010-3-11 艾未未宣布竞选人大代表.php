　　二、区县级人大代表有哪些法律赋予的权利？<br />
<br />
　　答：代表的权利有很多，也很大。依据宪法和相关法律规定，主要有：<br />
<br />
　　（1）审议权。审议是对列入会议议程的各项报告和议案进行讨论、发表意见、表明意愿和立场，给予肯定、否定或者提出修改意见的活动。<br />
<br />
　　（2）提案权。向代表大会提出属于本级人民代表大会职权范围内的议案；提出组织特定问题调查委员会。<br />
<br />
　　（3）表决权。表决，是指代表大会在通过报告和议案、决定有关事项时，由代表明确表示赞成或反对意见，并以法定标准来确定结果的行为。<br />
<br />
　　（4）选举权和罢免权。联名提名人大常委会主任、副主任和委员候选人，联名提名县长、副县长候选人，联名提名法院院长候选人，联名提名检察院检察长候选人，联名提名上一级人大代表候选人的权利；投票选举人大常委会主任、副主任和委员，选举县长、副县长，选举法院院长，选举检察院检察长，选举各局局长，选举法院副院长、庭长、副庭长、审判委员会委员、审判员，选举检察院副检察长、检察委员会委员、检察员，选举上一级人大代表的权利；罢免上述由人民代表大会代表选举产生或由人大常委会委员选举产生的国家机关的重要工作人员职务的权利；<br />
<br />
　　（5）询问权和质询权。询问和质询是国家权力机关对行政机关、审判机关和检察机关实行监督的形式。<br />
<br />
　　（6）建议权、批评权。人大代表的建议、批评和意见，是代表向本级人大或者其常委会提出的对各方面工作的看法、意见的总称。<br />
<br />
　　（7）人身特别保护权。在会议期间，非经本级人大主席团许可，闭会期间，非经本级人大常委会许可，不受逮捕和刑事审判。如果是现行犯被拘留，公安机关应立即向人大主席团或人大常委会汇报。<br />
<br />
　　（8）言论免责权。在人大会议上的发言和表决，不受法律追究。<br />
<br />
　　（9）物质帮助权。人大代表出席会议和执行代表职务，国家根据需要给予旅差费和必要补贴。<br />
<br />
　　区县级人大代表在闭会期间，有以下八项权利：<br />
<br />
　　(1)组织开展代表小组活动的权利；<br />
<br />
　　(2)视察“一府两院”工作的权利；<br />
<br />
　　(3)约见本级或下级国家机关负责人的权利；<br />
<br />
　　(4)提出临时召集本级人民代表大会的权利；<br />
<br />
　　(5)应邀列席本级人大常委会，应邀列席本级人大各专门委员会会议的权利；<br />
<br />
　　(6)听取人民群众的意见的权利；<br />
<br />
　　(7)参加特定问题调查委员会的权利；<br />
<br />
　　(8)对“一府两院”和人大常委会工作提出建议、批评、意见的权利。<br />
<br />
　　三、区县级人民代表大会有哪些法律规定的权力？<br />
<br />
　　答：县级人民代表大会职权有15项，可以概括为五个方面。<br />
<br />
　　1、保证宪法、法律和上级人大决议在本行政区域内执行，保证国民经济计划预算贯彻执行。<br />
<br />
　　2、对政治、经济、教育、科学、文化、卫生、环境与资源保护、民族等九方面的重大事项做出决定。<br />
<br />
　　3、选举本级国家机关的领导人员，选举上一级人大代表。<br />
<br />
　　4、对本级政府、法院、检察院进行监督：①法律监督；②工作监督。对乡镇级人大进行监督。<br />
<br />
　　5、保障法律赋予人民群众的权利，保护经济组织的合法权益。<br />
<br />
　　四、人民代表大会制度与社会政治文明的关系？<br />
<br />
　　答：人民代表大会制度的根本点是：人民代表大会是我国的最高权力机关；人民代表大会的权力来源于选民；公民通过人民代表大会行使国家权力；人民代表大会的代表由民主选举产生，对人民负责，<br />
<br />
　　受人民监督；行政机关、审判机关、检察机关都由人民代表大会选举产生，对它负责，受它监督。<br />
<br />
　　社会政治文明的根本点是：国家权力机关、行政机关、审判机关、检察机关的权力来源合法、合民意；同时，四大家机关的权力又受到其来源的有效监督和制约；公民的一切权利有明确的和真实的司法救济渠道保障。<br />
<br />
　　综上，人民代表大会制度与社会政治文明的关系是：公正、自由、民主的选举制度是国际社会公认的社会政治文明的基石。<br />
<br />
　　五、是否可以同时竞选县、乡两级人大代表？<br />
<br />
　　答：完全可以，这是合法的。比如有的乡镇长就既是乡镇人大代表，同时又是县、设区的市和省级人大代表。<br />
<br />
　　六、是否可以成立自己的竞选班子？<br />
<br />
　　答：法律没有禁止公民在竞选人大代表时成立竞选班子的规定。但可能有的地方政府有意认为竞选班子是民间组织，而没有在民政部门登记的民间组织是非法的，故政府以取缔非法民间组织而打乱候选人竞选秩序的可能性是有的。<br />
<br />
　　建议不要成立正式的竞选班子。但可邀请和接受志同道合者、亲朋好友等参与联名推荐自己为代表候选人，宣传自己的竞选主张及参选者的为人，监督正式代表候选人确定是否合法，参与和鼓励选民依法否定非法确定的正式代表候选人，核实委托投票人是否办理委托手续及被委托人是谁，等等。在选举日需要牢牢盯住应到选民数、实到选民数、实发选票数、实收选票数、委托投票数、在流动票箱投票的人和数、开票箱、唱票、监票、计票等。<br />
<br />
　　七、是否可以在本人选民名单公布以外的选区竞选人大代表？<br />
<br />
　　答：可以，这种做法完全合法。1998年8月31日，全国人大常委会法工委答复山西省人大常委会咨询时说：“直接选举县、乡人大代表时，选民十人以上联名推荐的代表候选人，不限于本选区选民，但应是本行政区的选民。选举本选区以外的本行政区的选民为代表，选举结果有效。同一选民在两个选区同时当选为代表的，应确定其在一个选区当选。”<br />
<br />
　　八、为什么要关注选区划分和各选区应选代表名额分配情况？<br />
<br />
　　答：有的县级选举委员会，利用其划分选区的权力，违法划分选区，其目的是使当选可能性很大的选民无法当选；县级选举委员会违法分配选区应选代表名额，在全国来说不是个别的。<br />
<br />
　　关注的目的：1、抵制和检举控告选举委员会违法划分选区和违法分配选区应选代表名额；2、选择对自己竞选和当选有利的选区。<br />
<br />
　　九、如何使自己成为代表候选人？<br />
<br />
　　答：在弄清楚了县级行政区域内共划分为多少个选区、各选区所辖单位和各选区分配的应选代表名额后，进行分析和比较，自己选择到某一选区竞选。<br />
<br />
　　使自己成为县级人大代表候选人，一点也不难，把握三条即可：1、你必须取得你所选择参选的选区所在的行政区域内的任何一个选区的选民资格；2、在“代表候选人推荐表”中签名的选民，必须是参选者选定的选区内的选民，选民10人联名不包括被推荐者自己，一张推荐表中被推荐的代表候选人人数不得超过该选区分配的应选代表名额；3、在选举委员会规定的交表期限内把推荐表送到选区选举工作组。<br />
<br />
　　十、为什么竞选者在成为代表候选人时争取选民签名的人越多越好？<br />
<br />
　　答：《选举法》关于正式代表候选人的确定的表述语意不清，缺乏可操作性。全国各地县级选举委员会普遍存在非法确定正式代表候选人的情况。<br />
<br />
　　在“代表候选人推荐表”（可使用若干份推荐表）中，竞选人获得选民签名的人数越多，其成为正式代表候选人越具有合法性。<br />
<br />
　　因为选举法规定只能“根据较多数选民的意见”或者“根据预选时得票多少的顺序，确定正式代表候选人名单”。<br />
<br />
　　十一、正式代表候选人是如何确定的？<br />
<br />
　　答：正式代表候选人的产生有三个环节。第一环节是提名推荐代表候选人；第二环节是各选民小组的选民讨论、协商正式代表候选人名单；第三环节是“如果所提候选人的人数超过应选代表名额一倍时，由选举委员会交各该选区的选民小组讨论、协商，根据较多数选民的意见，确定正式代表候选人名单；对正式代表候选人不能形成较为一致意见的，进行预选，根据预选时得票多少的顺序，确定正式代表候选人名单。”（见《选举法》第九条第一款<br />
<br />
　　十二、竞选者可以采取哪些介绍方式？<br />
<br />
　　答：没有竞争的选举是虚假选举，我国现行选举法鼓励依法向选民介绍候选人，我们的理解这种介绍实际就是一种竞选。<br />
<br />
　　在近几届县级人大代表竞选中，独立候选人一般采取了张贴竞选海报、散发竞选承诺、公开演讲辩论、告诉选民在选举日如何填写另选他人、打电话、发短信、寄电邮、挨家挨户拜访等方式。<br />
<br />
　　总之，“亲朋好友齐上阵，男女选民总动员；民意代表明白选，选战连天是文明。”<br />
<br />
　　十三、没有成为正式代表候选人的选民是否还有当选为代表的机会？<br />
<br />
　　答：有。在以前的县乡两级人大代表的选举中，这样的例子就非常多了。这样的例子在乡镇一级的选举中以前出现过许多，在区县级的选举中近年来也出现了一些。例如，湖北潜江的教师姚立法、枝江的农民吕邦列，四川泸州的工人曾建余、仁寿的农民张德安，重庆奉节的教师姚少凡，北京昌平的业主聂海亮，深圳的王亮等，都是非正式代表候选人竞选而当选的区县级人大代表。<br />
<br />
　　请注意，有的选区选举工作组的人所说的，根据选举委员会的意见，本选区选出的代表必须是女的且必须是中共党员之类的话，是误导选民，是违法的。<br />
<br />
　　十四、同一选区竞选者较多时怎么办？<br />
<br />
　　答：推荐代表候选人，有多种方式。包括选民10人以上联名可以提出，也包括“各政党，各人民团体，可以联合或者单独推荐”。在现阶段，只要选举过程被选民认为有些合法，或选民为了争取实现真正的民主选举，选民一般会自由组合10人以上联名分别推荐很多代表候选人，这样一来，选民10人以上联名推荐的代表候选人一定会比“各政党、各人民团体联合或者单独推荐”的代表候选人多很多。在这种情况下，建议竞选者或者各位竞选者委托牵头的支持者理性协商，以使选民们把选票投给那些为人正派、敢说实话、不畏邪恶、坚信真理、没有官瘾、信法守法和一心维护社会公正的民间政治家们，使他们被依法确定为正式代表候选人。或者使他们在“另选他人”栏得票从“票箱里跳出来”。<br />
<br />
　　总之，人民代表应当是人民的代言人，否则，休想得到神圣一票。<br />
<br />
　　十五、竞选者和助选团在投票日应做哪些事？<br />
<br />
　　答：竞选者和助选团牵头者（聘请顾问也行）必须十分熟悉法定投票程序和以往投票中常出现舞弊的环节及其做法。<br />
<br />
　　若投票日前的竞选、动员选民参与选举、监督批评阻止操纵选举者的工作，做得不具体扎实的话，投票日这天做再大的努力，竞选者是不可能当选的；但有一个可能会出现，那就是发现和抓住选举日是否有人在操纵选举的事实。<br />
<br />
　　即使投票日前的竞选、动员选民参与选举、监督批评阻止操纵选举者的工作已经做得比较具体扎实，但是投票日这天仍然有大量的工作要做，万万不能疏忽和轻“敌”。以下的一些工作是要认真做的：<br />
<br />
　　1、在投票日当天，除了那些因外出已经办理了委托投票手续和因老弱病残孕等原因已经申请到流动票箱投票的选民之外，还会有一些人因为某种原因没有到会投票，因此要有人专门对照选民名册，及时争取他们亲自到会或到站投票。<br />
<br />
　　2、要特别关注总监票人、监票人、唱票人和计票人，看他们是否在核实到会选民人数前由选民推选产生。对此，《选举法》第39条有明确的规定。<br />
<br />
　　3、助选团的人要监督投票的秩序，做好相关记录。比如核实选民人数。这里主要是指到会到站人数。委托投票人数可在发放选票时认真证录。在流动票箱投票人数，由跟随票箱的助选团的人如实记录。相关数据应及时汇总。<br />
<br />
　　4、要监督投票现场选民按照一定顺序排队接受身份检验，领取选票的秩序。否则，一人填写几十张上百张选票的事就可能发生。<br />
<br />
　　5、要监督选民必须进入秘密填票处填写选票的秩序。<br />
<br />
　　6、在计票前，应询问选区负责人，选区是否集中票箱到一处公开统一计票；是否洗票，即把选区所有票箱的选票集中起来，进行混合搅拌。若答复是不，则要及时电话与选举委员会办公室联系，请其纠正选区的错误做法。<br />
<br />
　　7、投票中不按规定书写的选票是常会出现的，如果选区工作组在告知选民如何划票时选民不大了解而出现划票错误，要即时通知选举委员会加以澄清。<br />
<br />
　　以往一些地方的选区选举工作人员，不正确对待“陪选”人员和在另选他人栏得票的竞选者的现象，助选团应尽可能在本次计票中及时发现，并告知选举委员会加以纠正。<br />
<br />
　　8、助选团还应注意一点，即有的选区唱票人不高声读出每张选票所选的被选人姓名；计票人不在垂直地面的黑板或者大纸上计票；一组唱票、计票和监票人员中，监票人只一人。如此现象都是不允许发生的，应及时向选举委员会报告，并要其纠正错误。<br />
<br />
　　十六、如果遭遇违法选举和破坏选举怎么办？<br />
<br />
　　答：破坏选举包括：①以金钱或者其他财物贿赂选民，妨害选民自由行使选举权和被选举权；②以暴力、威胁、欺骗或者其他非法手段妨害选民自由行使选举权和被选举权的；③伪造选举文件、虚报选<br />
<br />
　　举票数或者其他违法行为的；④对于控告、检举选举中违法行为的人，或者对于提出要求罢免代表的人进行压制、报复的。<br />
<br />
　　对破坏选举行为，应及时向县级检察院或县级公安局控告、检举。若他们不当事时，可向选举委员会、人大常委会、媒体反映。如该公安局管，而其不管时，可行政诉讼公安局不作为。<br />
<br />
　　对操纵、掌控选举，如分配代表名额、划分选区、选民小组组长产生、选民资格、确定正式代表候选人、委托投票、流动票箱、计票等中的违法问题，根据实际情况，可选择向法院起诉、向检察院控告、向选举委员会举报、向人大常委会申诉、向媒体投诉等。如能依法得到游行、集会许可，向非法者或有关组织以非暴力的方式示威，也是上策。<br />
<br />
　　上述方式方法仍不能解决问题，依法提出罢免“代表”要求，也是一条可选择的法治之路。</div>

							
							
							
							
