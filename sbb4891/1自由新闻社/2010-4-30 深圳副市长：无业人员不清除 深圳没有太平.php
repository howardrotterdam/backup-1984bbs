													<h2>深圳副市长：无业人员不清除 深圳没有太平</h2>
						
						
																											<div id="postmessage_590617" class="t_msgfont">南方日报<br />
“无业人员不清除出去,深圳没有太平。我们也在争取,深圳不是有立法权吗?如果给我们依据,对3个月以上无正当职业的人,不租房子给他,请他回原籍。(回原籍)至少还有块地方。否则在深圳怎么生活?”<br />
<br />
　　―――李铭<br />
<br />
　　深圳的治安为什么乱,其根源究竟在哪里?昨日,深圳市副市长、公安局长李铭与深圳市港澳界别的政协委员座谈,听取他们的建议意见,他对此问题进行了详尽阐释。他认为,流动人口总量失控,具体管理缺位,是深圳治安的症结所在。深圳的治安要从根子上解决,就是要解决流动人口总量失控,无业人员长期在深滞留等问题。不解决这个问题,深圳治安永无宁日。<br />
<br />
　　深圳人口年增百万<br />
<br />
　　李铭说,2003年孙志刚事件之后,大城市的收容遣送制度被取消。2003年,深圳的人口是700万人,现在是1400万,平均每年增加100万,刚好翻了一倍。“每年增加100万,深圳有这么多工作机会给他们吗?”李铭说,在2008年,深圳的流动人口1100万,去年,在经济危机的情况下,深圳的流动人口还是增加到1200万,又增加了100万。今年统计到现在,已经登记了1180万流动人口。采集率只有60%到70%。如果全部采集,流动人口总数可能达到1300万。<br />
<br />
　　“一个城市,人口以每年100万的基数在增,怎么管?在香港,如果按照这个数据增加,比深圳还乱。”李铭的话引起了现场港澳委员的热烈掌声。<br />
<br />
　　人口流动大难管理<br />
<br />
　　为什么深圳的治安难管?李铭分析,深圳外来流动人口流动性太大,变化太大,公安机关对他们的居住职业很难掌握。<br />
<br />
　　据公安部门统计,深圳的上千万流动人口中,每年离开的有100多万,新来100多万。“辛苦了一年,我们公安好不容易统计到这个人住在什么地方,什么职业或者无业,差不多搞完了,到年底春节前一退保,又换了。”李铭说,在剩下的1000多万人中,有30%的人每年换住地。很难掌握这些人住在哪里,靠什么职业谋生。“诸位换在我的位置上考虑一下,我连他住在哪里,靠什么谋生都不知道,我怎么管这些人?这个工作光靠公安不行。”<br />
<br />
　　目前,深圳已经成立了一支15000人的专职出租屋管理队伍,靠这批人难道不能实现有效监管吗?对此,李铭表示,出租屋管理办有15000人,但是目前他们被赋予了19种职能,街道的各项工作,包括计划生育等都要管,一年能登记两三次人口不错了。3月18日到4月18日,深圳全市大会战进行流动人口登记。新录入了200万人,销掉了150万人。什么概念?就是说,过去走了的没销掉,新增的还没掌握,这就350万人。这其中,新录入人员中,有80万是第一次来深圳。也就是说深圳每年有100万人新来。<br />
<br />
　　建议立法清除无业人员<br />
<br />
　　李铭说,深圳流动人口总量失控,具体管理不到位,是治安乱的原因。“来了也没问题,有职业,有收入就能正当过生活。”但是李铭表示,经过初步估计,深圳至少有100万无固定职业人员。<br />
<br />
　　“没有工作怎么办?公安什么也不干,盯着这100万人也管不过来。”李铭说,“无业人员不清除出去,深圳没有太平。我们也在争取,深圳不是有立法权吗?如果给我们依据,对3个月以上无正当职业的人,不租房子给他,请他回原籍。(回原籍)至少还有块地方。否则在深圳怎么生活?”<br />
<br />
　　李铭又给出了另一组数据来佐证他的观点。他表示,目前深圳各看守所在押的嫌疑人中,有88%是第一次犯罪。“他们就是生活所迫,在内地都是良民,在深圳找不到工作,生活不下去,铤而走险。”李铭认为,“无业人员怎么管,怎么有序,是解决深圳治安的关键点。不解决这个,治安没有办法。”李铭表示,光靠打击是不行的,防范和源头治理是治安管理的根本。<br />
<br />
■解决之道<br />
<br />
　　今后三五年再增5000警力<br />
<br />
　　警力不足一直是个困扰深圳发展的难题。昨日,不少港澳界别的政协委员都提出深圳应该增加警力。既然深圳有雄厚的财力,为什么就不能增加一些警察呢?<br />
<br />
　　对此,李铭进行了详细阐述。李铭说,警力配备是以户籍人口计算的,深圳现有警力19605人,万人警力只有13.5。与全国大中型城市相比,北京、上海、天津、广州分别是26.4、21.7、23.8和29.7,深圳仅仅是它们的一半。<br />
<br />
　　李铭说,增加警力的问题不是深圳市政府不想花钱,而是编制问题。他解释说,警察属于公务员,编制是中央管的。拿了编制才能招人。目前,深圳正在争取,在今后3到5年再招5000警力。<br />
<br />
　　李铭说,深圳的治安要管到香港的水平,在素质与香港接近的情况下,要满足深圳治安的需要,警力至少要翻一倍,也就是至少4万警力,才能基本把情况控制住。<br />
<br />
　　从严治警该立案不立要严处<br />
<br />
　　对于警队管理,李铭有八个字的要求,“从优待警,从严治警”。在昨日的座谈会上,李铭就对立案问题进行了详细的介绍。他透露,目前宝安区某派出所的所长就因为立案的问题被撤职。<br />
<br />
　　李铭在介绍警情时表示,从警情中的刑事警情来看,今年1到4月,刑事警情同比下降20%。李铭说,刑事警情是110打来的,谁也假不了。“立案是可以作假的,但是警情做不了假。因为老百姓打110,我们是百分之百的刑事警情要回访。”为此,市公安局从前年开始定了一条政策,警情如实录入,如实立案。凡是该立案不立案的,尤其是刑事案件,发现一宗处理一宗。<br />
<br />
　　李铭透露,最近,市公安局撤掉了宝安区某派出所的所长。原因是在倒查过程中,发现在几宗案件中,有警员和分管副所长没有把关,该立案没有立案。“我们就把他们撤掉了,全部要处分。”<br />
<br />
　　李铭说,为了防止不实立案的问题,市公安局把立案多少与考核脱钩。“就是立案多不说明你治安差,立案少不说明你治安好。如果该立的案不立,老百姓会觉得我们不作为,层层作假,将来公安就无法知道社会治安的真实情况。立多立少是客观存在的,不能人为限制。”</div>

							
							
							
							
