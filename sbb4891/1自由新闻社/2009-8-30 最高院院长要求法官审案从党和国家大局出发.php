<br />
王胜俊要求,要不断提高服务大局的意识。各级人民法院部署工作、处理问题、审理案件,都要从党和国家工作大局出发,努力寻求依法妥善解决的最佳方案。要坚持严格依法办案。严格依法办案,确保办案质量是人民法院审判工作的生命线,在任何时候、任何情况下都不能动摇。各级人民法院要严格从事实出发、按法律办事,把好事实证据关、适用法律关和审判程序关,把每一起案件都办成法院公正审判的样板,经得起历史和法律检验的“铁案”。要统筹兼顾程序公正与实体公正。在审理刑事大案要案时,要在确保实体公正的前提下,确保刑事审判活动严格按照法定程序开展,最大限度地彰显司法公正。要统筹兼顾司法公正与审判效率。效率是依法办案、司法公正的基本要求和应有之义,追求司法公正与提高诉讼效率作为刑事诉讼的价值目标,从根本上说是相辅相成、辩证统一的。刑事大案要案必须注意依法及时审结,维护社会主义法治的权威和尊严。要坚持实现法律效果和社会效果的统一。<br />
<br />
