													<h2>不要把讲政治顾大局庸俗化</h2>
						
						
																											<div id="postmessage_220438" class="t_msgfont">作者: 南方周末评论员 郭光东 <br />
2009-08-13 00:25:16 <br />
&nbsp; &nbsp; 司法部吴爱英部长8月6日的一席话引起舆论热议。她在全国司法厅(局)长座谈会上表示，要加强律师队伍教育管理，切实做好律师代理敏感案件和群体性事件的指导工作，教育引导广大律师讲政治、顾大局、守纪律，忠诚履行律师职责使命。<br />
&nbsp; &nbsp; 舆论以为吴部长讲话的不妥之处，大致有三点：一是有违反律师法之嫌，可能让律师从“为当事人提供法律服务的执业人员”重新退回到30年前的“国家的法律工作者”；二是认为律师严格按照律师法的规定履行职责为当事人服务，追求社会公平正义，就是最大的“讲政治”，超出这个范围的“讲政治”，可能会架空具体而明确的法律规范；三是觉得律师本来就是我们这个司法体制下的弱势群体，很多本该有的权利尚且难以保障，再强调讲政治、顾大局，势必让律师更被视作“讼棍”和捣蛋分子。<br />
&nbsp; &nbsp; 的确，以司法部部长之尊要求民间执业人士讲政治，多少显得突兀。不过，我们观察发现，讲政治、顾大局的要求，早已不是律师一个行业的事，各行各业管理层要求各自界别从业人员讲政治、顾大局，俨然成为一个普遍现象和流行趋势，成为各行、各级领导的一种惯性思维和习惯用语。<br />
&nbsp; &nbsp; 毋庸置疑，讲政治、顾大局作为一种从政素养，不仅适用于当今中国，即便放之古今中外，也是任何一位伟大政治家都需具备的。但是，从内在逻辑而言，讲政治、顾大局还有一个层次和格局的问题，绝非任何人都有资格、都有能力、都有义务去讲政治、顾大局。也就是说，讲政治、顾大局根本不是一个通过层层发包、传达到人就能最终实现的问题。<br />
&nbsp; &nbsp; 实际上，恰恰是因为某些地方政府、一些行业自以为是的讲政治、顾大局，导致问题最终演变到置政治、大局于不讲、不顾。具体到吴部长所讲到的敏感案件和群体性事件，我们不妨举例为证，演示当事人寻求问题解决的路径。<br />
&nbsp; &nbsp; 比如，有一群被污染企业侵害的村民，他们先找到地方政府，被官员告知企业为当地贡献了多少GDP，村民应该讲政治、顾大局；找到律师，律师告知他们，因涉及群体性事件司法局不让他们代理；找到法院，法官也以同样理由不予受理；找到媒体，记者也敢去采访，无奈逯军那样的官员会质问记者“是为党说话还是为人民说话”……一圈转下来，人人都要求他们讲政治，问题反倒没处讲，或许他们终于悟出，他们也得讲他们自己的政治、顾自己的小局，方法就是到街上讲，用谁都不愿见到的暴力去讲。于是，一个普普通通的小事就被逼成了一个敏感案件、群体性事件。<br />
&nbsp; &nbsp; 某些地方官员、职能部门的所谓讲政治、顾大局，往往只是在讲他们自己的利益和小局，是在把讲政治、顾大局口号化、庸俗化。更糟糕的是，只要把事搞大就能解决问题的可怕逻辑，变相鼓励了当事人制造敏感案件和大规模群体性事件。<br />
&nbsp; &nbsp; 在法律的框架内、在体制的管道内化解敏感和群体性事件，永远是最经济、最安全的危机处理模式。当事人被损害的利益总是实实在在的，那种借口讲政治、顾大局而把矛盾踢出本地本行业之外的懒汉态度，无疑是对执政党和国家的政治、大局的极端不负责。<br />
&nbsp; &nbsp; 其实，退一万步讲，即便有律师参与到群体性事件中，也总比我们常说的“一小撮别有用心的人”、“黑恶势力”要守法得多吧。<br />
