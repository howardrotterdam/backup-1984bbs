<br />
霍金警告说，外星人几乎是肯定存在的，但我们人类不要努力去寻找外星人<br />
　　新浪科技讯 北京时间4月26日消息，据《泰晤士报》报道，著名天体物理学家斯蒂芬・霍金警告说，外星人几乎是肯定存在的，但我们人类不要努力去寻找外星人，应该尽量避免与他们接触。否则，有可能给人类带来灾难。<br />
<br />
　　在为发现频道录制的一部最新纪录片中，霍金将表述他对一些宇宙最大神秘的最新理解。他认为，在宇宙的很多其他地方，外星人肯定存在，不只在行星里，还有可能存在于恒星的中心，甚至还可能漂浮在星际空间。<br />
<br />
　　霍金认为外星人存在的理由非常简单。他指出，宇宙有1000亿个星系，每个星系含有数亿颗恒星。宇宙如此之大，地球不可能是唯一拥有生命的星球。他说：“我从数学的逻辑来思考，只是这些数字就可以极其合理地考虑外星人的存在。我们真正的挑战是弄清楚外星生命长什么样子。”<br />
<br />
　　他认为，大部分地外生物等同于相当长时间里主宰地球的细菌或者简单生物。“探索频道”纪录片的一个画面显示，一个其他星球的崖面，成群的两条腿食草动物在吃草，并遭致很多会飞的黄色蜥蜴状食肉动物捕猎。另一画面显示，木卫二厚厚冰层下的海洋中，发光动物成群结队。<br />
<br />
　　虽然这类场景是构想出来的，但霍金用它们说明一个重点：少数生命形式可能是智能化的，但他们对人类是一种威胁，霍金相信，与这些生命进行接触对人类来说结果将是灾难性的。他认为，外星人可能会袭击地球掠夺地球上的资源，然后扬长而去，他说：“我们看看自己就知道智能生命是如何发展到无法自给自足的地步了。我设想他们应该是坐着大型飞船来到我们星球，不过由于长途旅行，他们已经耗尽了起飞时所带的资源。他们可能已经成为流浪者，伺机征服并殖民他们能够抵达的任何星球。”<br />
<br />
　　他的结论是设法与外星人接触“有点冒险”。他说：“如果外星人来拜访我们，我认为那么其结果就和当年哥伦布到达美洲大陆差不多，美洲的土著居民深受其害。”这部纪录片的完成标志着霍金取得一大成功，现年68岁的霍金因运动神经疾病变得瘫痪，沟通的能力极其受限。他和他的制作团队为这一节目筹划了3年，期间，他曾坚持重新改写剧本和监测摄制过程。“探索”频道的执行制作人约翰・史密森说：“他想要制作一个能令普通观众和科学家感兴趣的节目，那是一份艰难的工作，其中很多概念非常复杂。”<br />
<br />
　　霍金之前就曾表示存在外星人的可能性，他的观点一直通过一系列科学突破阐明，如这一发现，自1995年以来，人类已发现450多个围绕遥远恒星轨道运动的行星，这表明行星是一个常见现象。现在，天文学家发现的所有新行星都比地球大得多，但只是因为用来探测它们的望远镜不足以敏感到探测这类距离的和地球一样大的天体。另外一项突破是科学家发现地球生命已证明可居住在它的大部分极端环境。科学家推断，如果生命能在极端环境下生存和发展，那么可能没有不可能生存的地方。霍金对外星人的理解得到了很多其他科学家的支持。最近在他的英国广播公司系列片《太阳系奇迹》中，布莱恩・考克斯教授对他的这一理论表示支持，考克斯认为，火星、木卫二和土卫六可能是值得探究的地方。<br />
<br />
