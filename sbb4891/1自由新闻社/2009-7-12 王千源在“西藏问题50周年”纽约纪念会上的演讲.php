<br />
（演讲文稿）王千源：非暴力不合作之西藏未来发展前景探析<br />
On 三月 11th, 2009<br />
我今天演讲的核心可以概括为九个字：明相位，立德业，观全局。即知道相对位置关系，建 立功德伟业，树立全局观念。现在我将一一阐述。<br />
<br />
先谈明相位。万物变化永恒不息，处于何等位置就应采取相对的何等策略，才能顺天应时， 把握最佳机遇。我们首先须明晰历史相位。五十年前汉藏之间的冲突是两种截然不同的政权 和意识理念之间为控制权、领土权、军权、宗教权等而爆发的争斗。<br />
<br />
半个世纪过去了，如今汉人制藏，藏人失去在西藏的主导，汉藏冲突由统治权之争已经变为生存权之争。其次我们需明鉴关系相位。从西藏流亡政府角度讲，对西藏人民，达赖喇嘛远离故土数十载，千里万里，加之信息不畅，曾经的实际领袖逐渐转变为了精神领袖；对汉族人民，自九十年代起，中国政府一面高调宣传民族团结，一面强力孤立、异化西藏海内外自由人士，自由诉求被与分裂国家画上了等号，恐惧和排斥已经成为当今多数海内外汉人对达赖喇嘛的自然反应；对国际社会，达赖喇嘛是和平、理性、非暴力的化身，然而由于与中国政府关系的考虑，西藏问题异常棘手。中国政府早已汉化了西藏，对内坐拥强大的宣传工具和军事力量以弱其民，对外又兼以庞大的经济实力力削国际支持，外添描绘给国内民众的颇具魅力的和谐幻景，中国政府看似毫无必要与达赖喇嘛诚心谈判，西藏问题看似已经变为僵局。然而所有的事情都有转机。西藏人民在此弱势之时应藏锋蓄力，而非强力力争；达赖喇嘛应促进民族间的真正合作和解，而非如中国政府般的口是心非。他以骄，我以仁；他以诈，我以诚；他以力，我借力；他以斗，我解斗。唯此，方能赢得海内外人心。<br />
<br />
对于西藏问题的解决策略，我主张立德业，观全局。相位时时变化，德业永世不败。中国人 民与西藏人民本无冲突，中华文化与藏传佛教均承传仁心善举之道。五十年的误会并不会根除千百年的渊源，和平是汉藏民族共同的诉求。因而西藏流亡政府不应将汉人看为自己的对立面，因为真正的问题根源在于中共政府，而非人民。解决危机的根本途径必然在于最大程度的合作、包容和理解。如德业已立，那么纵观宇内，大凡民主自由的法制、人本为上的国家，民族冲突均能够在法律构架下相对平和地解决，曼德拉和奥巴马总统都是民族尊重团结互利的受益者和成功典范，而这也应是中华的蓝图。与之恰恰相反，苏联解体后的每个后共产主义多民族国家无不面临严峻的民族分裂考验：有的如南斯拉夫一分为六，有的如俄罗斯内战危机从未解除。这是历史的教训。当一个政权、一个政党，以斗争为纲，出卖国家利益，让民族自斗，贪污腐败，践踏生灵时，人民，唯有人民，这些利益不被保障，尊严不被维护的人民，才能够让世界和我们自己意识到，联起手来的我们是多么强大。<br />
<br />
