																											<div id="postmessage_44764" class="t_msgfont">《十亿消费者》中对《财经》的描写<br />
----------------------------------<br />
中国证券监督管理委员会（CSRC）于1992年10月成立。王波明在纽约中国商业联合会中的伙伴高西庆出任法律部主任，王波明本人则担任研究部主任，这是对他在纽约证券交易所多年研究经验的认可。但是王波明发现政府工作太官僚了。他觉得他和自己的朋友们已经完成了初步的目标，现在想要赚点钱，于是他把SEEC转成了生意。<br />
<br />
他建立了一个政府债券的电子交易系统，后来这个系统被政府关闭；他开始管理来自于国际投资人的规模一亿美元的基金，投资于那些希望上市的中国企业，并帮这些企业做广告。他还创办了《证券市场周刊》，后来成为中国股市最重要的信息来源。这本杂志很枯燥，通常都刊登技术和学术文章，但它是唯一经过证监会正式批准的刊物。杂志很快就取得了成功，立刻成为了中国最大的商业杂志，全国大约有一百万名订户。然而，越来越多的中国公司涌入股市，在股市开户的人数超过了四千万人，王波明意识到可以办一本更通俗、更全面的杂志。<br />
<br />
王波明和胡舒立曾在两年前略试合作，但是王波明很难与胡舒立站在一条线上。她总是提出一些不可能的要求来。时至今日，他知道她并未改变，自己的日子会很难过。但是他也明白舒立是中国最优秀、最进取的编辑。为了这本杂志，他必须全盘接受舒立的要求。市场已经准备好迎接一本勇敢、正直的杂志，而舒立就是那个出版人。王波明还知道曾协助证监会成立的中国官员――王岐山和周小川，现在都在银行和金融领域担任高位，他们也会重视通过真正的媒体监督保护投资人的利益。而在另一方，胡舒立则认为她可以利用王波明充足的财力来发行一本诚实正直的杂志，积极推动中国金融市场的监管。<br />
<br />
但是在答应王波明的合作邀请之前，胡舒立列出了严格的条件。她提出了编辑人员的预算，其中包括每年二十万美元的记者工资，这样她就可以确保记者的诚实。王波明没有退缩。然后舒立使出了第二击：“我来负责所有的编辑工作。你不能干预。在编辑和广告之间需要建立一道中国墙。”<br />
<br />
王波明接受了舒立的条件，她就开始招募人员，要把他们训练成真正的记者。她从《中国商业周刊》带来了一些值得信任的同事，但她也寻找那些在编辑或记者行业工作时间不长的年轻人，他们尚未腐败，也还没来得及落入记者们通常会经历的自我审查的陷阱中。一当人们听说胡舒立要开办一本新的杂志，名为《财经》，全中国的记者都给她发来了应聘的邮件。<br />
<br />
怒气冲冲的电话<br />
1997-98年的亚洲金融危机让中国领导人看到了狼狈为奸的资本主义如何摧毁一国的金融体系、让数十年的发展与增长灰飞烟灭。《财经》杂志1998年4月份的创刊号中，详细揭露了一家名为琼民源的上市公司的故事，这家倍受热捧的公司刚因提交虚假财务报告而暂停交易。通常，证监会禁止中国记者报道此类公司的问题，理由是这样的丑闻会打击投资人的热情、阻碍中国股市的发展。政府依赖股市将大量的个人存款注入到国有企业的融资和改制中去。王波明不得不派人去证监会，整整一个礼拜都为这篇报道进行道歉。但是他的新杂志一举成名，证监会很多官员对刊物的激进姿态都表示欢迎。<br />
<br />
舒立和她的记者们继续报道股市问题，并请求专家撰写经济政策分析，质疑某些政府政策。结果不言而喻――王波明开始接到越来越多怒气冲冲的电话。有一段时间似乎每个他认识的人都在生他的气，生他的杂志的气。监管者抱怨《财经》打破了新闻报道的禁令。上市公司管理层大叫《财经》在狙击公司的股价。投资银行为《财经》揭露正在寻求上市的公司的不利消息而苦恼。对于一个自诩和每个人都是朋友的人来说，这样的电话令王波明痛苦。他会有礼貌的听完对方的话，然后解释说他只是拥有这本杂志，但是不能干涉编辑的决定。打电话的人会感到很困扰。你拥有它，但是你不能决定发表什么？告诉舒立让她缓和甚或毙掉批评自己朋友的文章――王波明感到羞于启齿，同时对她可怕的脾气也心怀忌惮。不管如何，当客户因为批评文章而撤下广告时，王波明很痛心。<br />
<br />
中国的商业社会很快就学会了害怕并尊重胡舒立。她从不打高尔夫球，也很少参加社交活动。但是就算她和商业人士没有共同的休闲时光，她和他们却有着共同的改革中国的目标。她说话快，走路快，但无论是她的嘴还是她的脚都没有她的脑子快。没有闲聊的功夫。她的很多朋友都已经习惯接到她的电话，吩咐他们为她做这做那，而她提出要求的口吻是如此确凿，没有一点不好意思，甚至都没有人敢向她提出疑问。她简单地认为全世界都是为她服务的，但只是为了让这个世界更美好。很多政府官员和金融界经理们都搬到了北京城东现代化的高楼大厦之中，胡舒立却依然住在50年代苏联风格、楼梯间阴暗破旧楼的水泥楼房里。她家中只有很少几件基本的家具，但是四面墙都摆满了中英文的历史、金融和政治方面的书籍。<br />
<br />
舒立的员工对她充满敬意，但是她对他们则非常粗率。“她没有管理天赋。她不喜欢管理一个组织。她是一名记者。她总是想要抓住新闻。”一名《财经》记者说。“她设定了很高的标准，有时候她简直就是折磨自己的下属。她总是在推啊、推啊、推。她永远不会停止，直到你抱怨。然后她会后退一点。我们和新来的记者说，如果她推你推得太远，而你又不抱怨，那就是你的问题了。”<br />
<br />
舒立很早就意识到对出版物进行审查的共产党官员对经济知之甚少，对金融更是一窍不通。王波明任命了一名前新华社编辑在每期《财经》杂志发行前进行审稿，舒立就在每期杂志中都加上一篇能引起这名编辑争论的有关政治的报道。这位编辑算是有了一件可做之事，与此同时舒立则在他眼皮子底下通过了能引起争议的商业报道。<br />
<br />
她靠的就是争议。2000年10月，《财经》发表了一批报道，揭露了中国共同基金的经理人串通起来操纵股市，引起了中国整个金融行业的震动。报道的核心是上海证券交易所一名官员撰写的一篇秘密报告，政府感到非常为难，因此把它压了下去。舒立搞到了这份报告，给其中的信息加上了分析和解释，丑闻被曝光了。中国主要几家证券公司进行了反击，在新华社发行的证券市场报纸上联合谴责《财经》杂志。<br />
<br />
一年以后，2001年8月，《财经》再次引燃了金融行业，深入揭露了银广厦的故事，这家公司的股价当时正直线[升，是股市投资人热烈追捧的明星股。公司CEO在文章发表前找到王波明，提出购买广告来撤换这篇文章，后来由提出收购杂志。这家公司是江泽民主席和其它国家领导人曾热烈赞扬并访问过的公司。王波明把舒立找来问她有关这篇报道的事情。她向他保证自己有铁一样的证据证明这家公司财务报表做假。王波明对这件事的政治影响依然很担心，他给老朋友王岐山打了电话。王岐山当时是国务院经济体制改革办公室主任，他答复道：“公司是真的还是假的？如果是假的，那就报。”<br />
<br />
《财经》报道说银广厦在财务报告上作假。股价从34跌到了7。证监会进行了调查，报告说公司的财务报表远比《财经》报道的要夸张。公司把《财经》告上了法庭，但是两次都败诉。<br />
<br />
《财经》引来了很多模仿者，其中最好的都是曾为舒立工作过的编辑离开《财经》后创立的。很多时候，他们的努力受到了出版商的阻挠，出版商认为揭露和激进的新闻报道只会带来麻烦。虽然有一些亮点，但《财经》依然是中国为数不多的不以赞美文章换广告或金钱的出版物之一。<br />
<br />
2003年春天，中国遭到了致命的呼吸道传染疾病SARS的袭击。《财经》走出了自己传统的经济领域。当广东省有人开始死亡时，中国政府下令所有媒体不得报道此事，而只是印上政府的官方说法，即SARS是被隔离的小问题。流言传播得很快，中国国民开始恐慌起来，他们正处在传染病的中心。《财经》跳入了视野。舒立去到了中国西部省份，访问那里的官员，寻找疾病爆发的形式。她派记者到香港和河内，视察那些SARS热点地区。她甚至刊登了一名加拿大记者报道的关于多伦多出现SARS的文章。杂志把亚特兰大美国疾病控制和防疫中心视为中国政府应学习的榜样。《财经》甚至出版了特别周刊深入报道SARS。<br />
<br />
“舒立知道在这样的危机时刻，正是一名新闻记者应该有所突破的时候。”一名《财经》编辑说道。“我们知道我们有机会，因为情况太不清楚了。今天我们可以积极地报道股市，明天我们就会报道平民选举。”<br />
<br />
这对你意味着什么<br />
20世纪90年代，当我担任华尔街日报中国代表处主任的时候，我震惊地发现许多中国记者其实都知道这个国家到底发生了什么，但却只有极少数事情他们能够真正予以报道。在中国，胡舒立并不是唯一一个有着远大的理想、强烈的正义感、以及发现并公布真相的决心的记者。我看到了很多编辑和记者，还有一些出版物，勇敢地挖掘那些政府并不想说的故事。也许程益中的话最能描述中国记者的命运。程益中是广州《南方都市报》的编辑，39岁，原则性强而又勇于冒险。2004年夏天，他因捏造出来的参与报纸经营费用贪污的罪名被当地共产党官员逮捕。真正的原因是当地官员想要阻止这家报纸的激进报道。当时报纸刚刚揭露了一名大学毕业生在警察局拘留所死亡的事件，让他们极为不快。这篇报道引发了全国人民的愤怒，并最终导致北京修订了警方拘留的相关法规。程益中在监狱中呆了五个月，未经历任何审讯，最后在退休官员、律师和记者的积极活动下被释放。当他获释时，程益中如此描述中国的新闻行业：“自由意味着知道你自己的笼子有多大。”<br />
<br />
无论是刘长乐还是胡舒立都有着敏锐的感觉，知道他们的笼子有多大。他们知道政府想要什么，这个国家又需要什么才能进步发展。舒立的笼子是证券市场和经济政策。刘长乐的笼子是把国际新闻带入中国，并在全世界展现中国的形象。但是两个人又时刻保持着政治嗅觉，随时寻造可以扩大自己笼子的机会。<br />
<br />
不幸的是，刘长乐和胡舒立在中国新闻行业的建设中是例外，而不是普遍存在。大多数新闻记者腐败、笨拙而懒惰。他们对涉及到政治、政府和社会问题的新闻是如此的无能为力，只能把关注点放在商业新闻上。但是在很多情况下，商业新闻只是成了为自己赚钱的一个工具，他们和中国的公司老总们串通起来抬高股价或帮助中国公司攻击其外国和国内竞争对手。天安门事件后席卷中国的民族主义情绪也不断的影响着商业界。所以只要他们在帮助中国公司，或仅仅是对外国机构大放厥词，中国记者一般都可以自由地攻击外国公司。印刷出版行业通常被分成商业媒体和党的宣传喉舌两大块。他们都有商业和金融版面。广告投放者需要拥有购买力的读者。因此党报渐渐被淘汰，演变成了付费喉舌。编辑和记者收钱为公司进行报道。</div>

							
							
							
							
