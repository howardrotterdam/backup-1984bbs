																											<div id="postmessage_44535" class="t_msgfont">央视新址大火余波未了<br />
<br />
本文见《财经》杂志 2009年第5期 出版日期2009年03月02日 <br />
<br />
<br />
<br />
元宵夜的火灾，亦使公司化运作和新址建设中的“黑洞”端倪渐显<br />
<br />
<br />
《财经》记者 欧阳洪亮 罗昌平<br />
<br />
央视新台址建设公司化运作简图<br />
<br />
<br />
　　在中央电视台新台址（下称央视新址）大火发生半月之后，指向渎职侵权的调查已获新的突破，共约20名涉案嫌犯被北京市GA局采取措施。<br />
<br />
　　2009年2月9日晚8时20分左右，农历元宵之夜，央视新址因其工作人员擅自雇用烟花公司违规燃放A类烟花，导致北配楼起火并持续近六个小时。事故导致一死七伤（参见《财经》2009年第4期“追问央视新址大火”）。<br />
<br />
　　案发当夜22时许，央视副总工程师、新台址建设工程办公室（下称央视新址办）主任徐威，即被北京市GA局朝阳分局呼家楼派出所带走，监控在一家宾馆。相继被查的还有现场三名央视新址办工作人员，以及负责现场燃放礼花的湖南浏阳三湘烟花制造公司的八名员工。上述12人因涉嫌危险物品肇事罪，成为第一批被刑事拘留者。<br />
<br />
　　2月19日，北京警方再刑拘五名涉案嫌犯。其中包括运输烟花爆竹的司机，以及帮助联系现场燃放公司的中间人等。<br />
<br />
　　北京市警方、检方的调查并不止于渎职侵权，还延展至央视新址工程的经济问题。《财经》记者从多个渠道获知，承揽央视新址工程B标段（即北配楼）的北京城建集团一名项目负责人受到办案部门调查；中央电视台排名第五的副台长、央视新址园区法定代表人李晓明亦被调查。<br />
<br />
　　《财经》记者调查确证，上述约20名被调查人员中，央视新址办原主任徐威、新址办综合业务处副处长邓炯慧、北京大新恒太传媒科技发展有限公司（下称大新恒太）董事长沙鹏，均已聘请辩护律师。沙鹏同时也有央视员工身份，围绕其名下公司在央视新址工程中的大宗业务，成为调查机关线索突破的关键所在。<br />
<br />
徐威身后<br />
<br />
　　据《财经》记者从接近办案的人士处获悉，徐威在被刑事拘留后，交待了诸多问题，已经超越简单的火灾渎职行为。据透露，徐威交待的问题涉及央视新址建设诸项工程招投标经济问题，“徐威之后还有谁，尚未可知”。<br />
　　中央电视台现任台长赵化勇1999年2月主政央视以来，中央电视台经营业务向公司化、多元化方向发展。央视新址建设亦以公司化模式进行。2008年2月，赵化勇曾在中央电视台工作会议报告中称，2003年正式启动的新址建设工程，经过五年奋战，已经取得决定性胜利。<br />
<br />
　　一年后的此番大火，则使公司化运作和新址建设中的“黑洞”端倪渐显。<br />
<br />
　　根据现行体制，隶属国家广电总局的中央电视台，为副部级事业单位。其最高决策机构为央视党组，下设行政、采编、经营、技术四个委员会。在行政委员会之下，2001年3月新设立央视新址筹建办，由央视副台长李晓明任法定代表人，副总工程师、技术管理办公室主任徐威兼任主任，负责新址建设筹备。2005年获得广电总局批文后，新址办正式成立，徐威开始全面负责央视新址建设。<br />
<br />
　　对应于此，2003年，央视与北京市第一家专门从事建设项目全过程管理的企业――北京国金管理咨询有限公司，联合成立了北京央视国金工程管理有限公司（下称央视国金），对央视新址建设实行公司化运作，由徐威任法定代表人。徐威得以掌控央视200亿元新址建设资金的运作。<br />
<br />
　　地处北京CBD核心区域的央视新址园区，于2005年4月28日开工，内含位于园区西南侧的CCTV主楼、西北侧的TVCC电视文化中心（即起火的北配楼，含文华东方酒店）、东北角的能源服务中心。早在2001年，原国家计委 “[2001]2795”号文批复，央视新址工程项目获准立项，总投资76.66亿元；其中央视自有资金55.17亿元，银行贷款21.49亿元。<br />
<br />
　　此后，央视新址建设工期一延再延，项目资金也一再上涨。至2004年8月，国家发改委的批文增加资金至78.91亿元。至今，央视新址工程的建设投资已达120余亿元，另有70多亿元新设备费用。据悉，央视搬迁新址将全部使用新设备，原设备将留原址备用。<br />
<br />
　　在担任新址办主任之前，徐威在央视任职近20年，一直为技术人员，曾多次参加央视的大型宣传节目和重大报道活动的技术方案制订和组织实施工作，自2000年12月起任技术管理办主任。<br />
<br />
　　为何新址建设没有让基建处或者后勤部门主抓，而让一个技术负责人牵头？熟知内情的央视内部人员称，原因之一可能是徐威与央视副台长、央视新址园区法定代表人李晓明是大学同学，关系密切。<br />
<br />
　　在周围人的印象中，徐威为人灵活而豪爽义气。其中学同学忆述：“每次同学聚会，都是徐威为大家埋单。”在同学眼里，徐威当年在班上并不起眼，但毕业后混最好。<br />
<br />
　　《财经》记者获悉，2月9日央视大火当晚，李晓明即被办案部门传讯，旋即回家。第二天，李再度被传讯。接近李晓明的人表示，火灾后，副台长李晓明接受了办案部门调查，但并未被控制，2月24日，依旧见到李晓明在台里上班。<br />
<br />
烟花牵出关联公司<br />
<br />
　　元宵夜的烟花不仅是火灾元凶，亦是经济问题调查的主要突破口。<br />
<br />
　　从2007年开始，央视连续三年安排在新址园区燃放烟花，其中2007年放了价值约30万元的烟花，2008年放了约50万元的烟花。此次引起大火的烟花燃放号称100万元，据央视内部人士透露，实际价值约35万元。<br />
<br />
　　据《财经》记者从业内了解，烟花行业回扣高达30％。经销商获利丰厚。<br />
<br />
　　接近案件的人士告诉《财经》记者，央视新址办多次燃放的烟花，均采购自浏阳三湘公司。初步调查，仅此批烟花，徐威即从三湘公司获得约8万元现金回扣。这成为徐威涉嫌经济问题的直接突破口。<br />
<br />
　　进一步调查发现，央视新址办采购此批700余发烟花，全由大新恒太走账。火灾发生当晚，大新恒太董事长沙鹏被控制，接着被刑事拘留。<br />
<br />
　　大新恒太业务主要为针对央视新址建设提供设备、软件、系统设计、系统集成等。大新恒太成立于2006年1月，500万元注册资本由央视国金全资注入，徐威为该公司第一任法定代表人。自此之后，大新恒太走上了一条由内部人控制的“私有化”之路。<br />
<br />
　　2006年4月12日，央视国金将所持股权转让给影响传媒有限公司。影响传媒有限公司亦长期依托于央视承揽广告、咨询和培训业务。<br />
<br />
　　不过，在成功中标央视的多哈亚运会转播设备采购项目、“专用采编四（SATA）”项目两笔业务后，影响传媒有限公司即从大新恒太全身退出，将股权转让给李、赵军、沙鹏、沈旭、赵序霞五名自然人。<br />
<br />
　　《财经》记者查证，李又名李明桦，系沙鹏女友，现任央视国金财务部副经理、央视文化中心财务总监，为徐威下属；赵军、沙鹏亦为央视新址办员工，五人均与徐威关系亲密。<br />
<br />
　　此后的2007年，大新恒太公司的股东变动频繁。当年7月9日，沈旭、赵序霞退出，黄丽芳进入；11月17日，北京亚细亚智业科技有限公司向大新恒太注资500万元，使后者注册资本增至1000万元。2007年11月23日至今，李、黄丽芳、亚细亚公司退出。此后，该公司股权再未发生变化，1000万元注册资本由五名自然人分持，其中，罗凤娇330万元、赵军310万元、沙鹏180万元、李小冬150万元、赵序霞30万元。<br />
<br />
　　上述人员中，赵军、赵序霞两人均为央视新址办员工，且在关键部门任职；其中，赵序霞系徐威在电视技术设备和软件工程方面多年合作的伙伴，在新址开始建设后，调入新址办。<br />
<br />
　　根据公开资料显示，大新恒太成立三年以来，至少经营了与央视新址工程有关的七宗业务，标的总金额过亿元。如2007年，仅在央视新址工程，即承揽了弱电工程B标段及A标段服务楼弱电网络系统设备材料供应项目、基础应用系统集成和软件开发项目；2008年，又获得奥运节目光传输设备，以及新址工程中的B标段酒店客房AV系统设备材料招标项目；等等。<br />
<br />
微妙的“责任分担”<br />
<br />
　　据《财经》记者了解，在徐威、沙鹏被刑拘后，央视有关方面即为两名当事人聘请了代理律师。<br />
<br />
　　其中，徐威的律师在接手此案之后，曾就责任归属提出三点法律意见：一是此工程是否交付，若未交付，施工方是否承担责任；二是当夜燃放烟花，是徐威个人行为还是单位行为；三是烟花公司在A类烟花的运输与燃放过程中是否担责。<br />
<br />
　　接近案件调查的人士告诉《财经》记者，目前如何鉴定这三方的责任，专家组仍存在争议。据初步核查结果，徐威的行为显然是单位行为。当晚烟花燃放时，央视安排了四台专业摄像机，“现场录制焰火中的央视新址，以备存档今后用于电视节目中”。<br />
<br />
　　上述人士介绍，目前偏向于业主单位央视负主要责任，施工方、烟花公司负不同程度的次要责任。<br />
<br />
　　湖南三湘烟花制造有限公司目前被刑拘的员工共九人，为所涉当事各方身陷最深者。2月17日上午，三湘烟花公司董事长戴剑虹在电话中告诉《财经》记者：“北京市GA局已派人来公司，正在调查此事。”<br />
<br />
　　据新华社报道中引述的北京警方人士的话称，在新近五名被拘留人员中，包括运输烟花爆竹的司机、帮助司机躲避警方检查绕行小路的带路人，以及帮助联系现场燃放公司的中间人等。根据《北京市烟花爆竹安全管理规定》，北京对烟花爆竹实行专营制度，单位和个人必须从具有许可证的销售网点购买烟花爆竹，私自从外省市购买烟花爆竹被明令禁止。非但如此，北京市还规定，在该市行政区域内运输烟花爆竹，应当取得GA机关的运输许可，否则不得运输。<br />
<br />
　　不过，针对三湘烟花公司的责任，各界说法不一。徐威的辩护律师认为，烟花公司应该承担运输和燃放A类烟花的申报责任；但反对的声音认为，三湘烟花公司受雇于央视新址办，后者才是直接的责任方。<br />
<br />
　　随着施工方北京城建集团一名项目负责人被调查，此案又已引起对工程建设与实施的关注。<br />
<br />
　　2月13日，央视召开高层会议，通报了火灾现场勘察的初步结果：央视新址北配楼着火后，燃烧主要集中在钛合金下面的保温层，具有表皮过火的特点。大楼保温层使用的材料是国家推荐使用的新型节能保温材料，这种材料燃烧后过火极快，因此瞬间从北配楼顶部蔓延到整个大楼，这次火灾是新中国成立以来建筑物过火燃烧最快的一例。<br />
<br />
　　北京市消防局副局长、新闻发言人骆原告诉记者，火灾蔓延如此之快，与建筑材料、建筑物高度都有直接关系。<br />
<br />
　　据北京市消防局提供的资料，起火的电视文化中心共30层，高159米，建筑面积103648平方米，主体结构为钢筋混凝土结构，外立面装修材料南北侧为玻璃幕墙，东西立面为钛锌板，外墙保温材料为挤塑板等。<br />
<br />
　　由此看来，此次大火虽为“人祸”，但大楼防火不达标的保温材料“挤塑板”亦是“帮凶 ”。此次失火的央视新址B标段，其幕墙工程由中山盛兴股份公司承建。2005年11月21日下午，央视和中山盛兴关于北配楼幕墙工程签约仪式在北京建国门外长富宫饭店举行，央视副台长李晓明亲自出席签约仪式。<br />
<br />
　　中山盛兴股份公司副总经理冯国敏对《财经》记者表示，有关幕墙防火材料是否达标的问题，相关部门已经在进行调查，目前不方便表态。《财经》记者获得的一份《防火审核意见书》显示，北京市消防局曾对央视新址共提出26条消防整改措施，其中八条涉及北配楼，如“地上14层采用扩大防烟前室代替避难层”，“5层、27层、28层厨房所用的燃气管道要从地下一层进线处穿越防火分区”等，均不符合防火规范。可见，施工方在消防方面的疏忽，以及元宵之夜未劝阻业主方燃放烟花，也存在责任。<br />
<br />
善后之局<br />
<br />
　　而今，火后的央视新址北配楼仍被封锁，一侧的三环路边，常有人驻足留影。其是拆是修，为人们所关注。<br />
<br />
　　2月13日，央视召开高层会议内部通报称，专家组认为，北配楼过火后，由外墙往里损失逐渐减轻，越往外损失越严重。大楼朝南、朝北方向的窗户玻璃没有破碎，室内的物品基本完好。玻璃破碎的房间大都只在窗户附近有火烧情况。“初步判断，大楼主体结构基本没有问题，挂幕墙的金属网架整体也基本完好，钢筋混凝土结构没有受到损伤，承载屋顶的桁架没有发生明显变形，但个别部位需要修补。”<br />
<br />
　　然而，央视此次会议所述的“专家组”，邀请的是建筑专家。他们初步形成的结论不具有法律效力，亦未获得建设、消防主管部门的认同。<br />
<br />
　　据《财经》记者了解，建设部、北京市消防局的联合调查组，国家安监总局的独立专家局，仍在进行勘察工作，目前均未正式发表调查结论。一位建筑装饰专家对《财经》记者表示，他对央视内部通报持谨慎态度，认为其中某些细节难以置信，比如“ 大楼朝南、朝北方向的窗户玻璃没有破碎的，室内的物品基本完好”，是“基本不可能的”。<br />
<br />
　　接近调查组的一位人士称，一名受央视邀请现场勘察的建筑专家口头表达了两条意见：长达六小时的高温燃烧导致结构强度大大下降，受力状况明显改变，安全隐患很大；作为全球瞩目的中国标志性建筑，加之处于华北地震带和北京中央商务区，不容许再次发生任何意外。<br />
<br />
　　“烧了五六个小时，楼的主体结构也差不多了。”参与北配楼前期设计的建筑师吴朝辉说。<br />
<br />
　　另一位主张拆除大楼的建筑专家告诉《财经》记者，如果不拆除，修补的资金远比拆除的少。但对未来的央视接任者来说，不拆除，可能意味要时刻应对一个“定时炸弹”。<br />
　　大火和徐威被拘，亦使得央视电视文化中心面临着善后之局。<br />
<br />
　　失火的央视电视文化中心（TVCC），由央视所属的中广影视卫星有限责任公司与中国国际电视总公司于2007年4月共同出资组建，是新址建设工程以及中央电视台媒体经营业务的重要组成部分。至失火时，电视文化中心已完成投资20多亿元；2009年年前，央视资产登记时，核定为30亿元。<br />
<br />
　　电视文化中心核心产业为央视华汇时代剧院和文华东方酒店。文华东方酒店拥有241间豪华客房及高档配套设施，原计划酒店于2008年中开业，但一直拖延到2009年火灾被烧毁，依旧没有开业。大火过后，酒店开业将无限期后延。<br />
<br />
　　央视华汇时代剧院在2008年完工开始承揽演出业务，作为开幕大戏的《陪我看电视》已经签约并进行广告宣传，原计划于2009年3月18日上演，演出票都已经售出。火灾后，必须临时终止演出更换场地，央视电视文化中心将面临系列合同纠纷。<br />
<br />
　　2月13日，中央电视台台长赵化勇主持召开的央视高层会议决定，央视新址主体大楼的搬迁计划不变，仍在今年国庆节前后陆续进行。<br />
<br />
　　央视内部人士告诉《财经》记者，61岁的赵化勇本应在2008年正常退休。火灾发生之后，赵化勇全力投入善后事宜，中央电视台日常工作由第一副台长张长明全权负责。<br />
<br />
　　据悉，国家审计署2009年专项计划中，将包括对中央电视台管理层的离任审计，并延伸审计央视新址工程。■<br />
