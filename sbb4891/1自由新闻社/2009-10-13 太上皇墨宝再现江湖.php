&nbsp; &nbsp;江泽民的题词和这些人有什么不同呢？<br />
&nbsp; &nbsp;如果有所不同，就是在野外题字的人是在偷偷摸摸地进行。而江泽民则会以国家主席的身份堂而皇之的当众挥笔。而且可以想象每次题词都能引起一片喝彩。某些基层领导人，还会从“题词”中找出很多“精神”和“意义”。成为日后作报告的内容，成为自己政绩的佐证。江泽民题词之后，很快就会在各类报刊上，在电视上出现。在旅游景点的题词，也会放大之后，摆在显眼的地方。<br />
&nbsp; &nbsp;表演之欲人皆有之，但要分清场合，演员到舞台上表演，书法家去书展。江泽民写字太一般，不该利用职权去到处表演。此风一开，野外题词不会少。野外题词破坏景观，污染环境，受到众人非议是理所当然的，对江泽民主席的题词也不能只是歌颂。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;**制造个人崇拜的能手**<br />
&nbsp; &nbsp;江泽民利用各种方式制造对他的个人崇拜。其中到处题词创下了记录。<br />
&nbsp; &nbsp;想当年“文革”之中，毛泽东一招手就会引起欢声雷动，热泪盈眶。因为当时信息被封锁。知识分子的嘴巴上贴了封条，张志新等先知先觉者，被割断了喉管。人民大众有耳不能听，有眼不能看，处于愚昧状态之中。只能由著毛泽东去造势。<br />
&nbsp; &nbsp;此一时彼一时，现在到底不同了，自由知识分子发出了独立的声音，人民大众开始觉醒。江泽民用“核心”的名义，用题词的声势，用电视的频频出镜，用宣传工具歌功颂德、封锁批评言论，来制造个人崇拜，现在看来，这些办法不但不能达到正面的效果，而且适得其反，遭到大众的厌恶。有人说只要电视上出现江泽民的形象，他就急忙调换频道。<br />
&nbsp; &nbsp;五十年来，人们从自己的亲身体会，逐渐知道，个人崇拜是怎么回事。<br />
&nbsp; &nbsp;对领导人的表演也都有了一些分析能力。反对制造个人崇拜的人，将会越来越多。<br />
&nbsp; &nbsp;记得八十年代初，“老一代革命家”，在批评华国锋时，曾说他到处题词，制造个人崇拜。没想到二十年后的江泽民竟青出于蓝胜于蓝。江的题词大概是华国锋的48倍。不知当今的“老一代”对此有何评价？<br />
&nbsp; &nbsp;如果政治不改革，只怕江泽民的题词记录还会被别人打破。<br />
&nbsp; &nbsp;<br />
孙文广 2003－9－20于山东大学</div>

							
							
							
							
