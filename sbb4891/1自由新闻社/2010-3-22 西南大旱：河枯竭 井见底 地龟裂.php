<br />
专家指出 3大气象因素<br />
<br />
四川省气候中心主任马振峰表示，西南地区此次发生干旱，主要有三大原因：一是青藏高原去年冬天积雪偏少，南亚季风弱，输送到西南地区上空的水气不充沛，加上北方南下的冷空气偏东偏北，造成西南地区出现干旱；二是厄尔尼诺现象，东亚季风携带的水气与北方南下的冷空气相遇，导致东部降水多，北方降雪多，西南降雨少；三是今年北方冷空气南下大多偏东偏北，同时因秦岭阻挡，很难越过高山进入西南并形成降水。<br />
<br />
国务院发展研究中心农村部研究员崔晓黎认为，西南特大干旱原因复杂，除了气象因素之外，与一直以来生态环境遭受破坏等问题有关。<br />
<br />
内地网民翻出 2004年 7月 9日《中国青年报》报道，该报道披露，印度尼西亚造纸巨头金光集团在中国西南投资大型造纸厂，为获取造纸资源，该集团骗得中方同意，在云南和广西两地大肆砍伐原生林木，改种生长快速、用于造纸的桉树。因桉树需要大量地下水，「它们就如同一台台抽水机，吸干了地下水」。网民质疑大旱或与此有关。<br />
