													<h2>中国评论s忧旱情农产 更忧通胀</h2>
						
						
																											<div id="postmessage_526389" class="t_msgfont">明报网上版 2010-03-22<br />
中国评论s忧旱情农产 更忧通胀<br />
<br />
【明报专讯】西南大旱，总理温家宝并非首度关注，农历年间他就曾到灾区广西视察。但今次云南视察时他特别指出，农业生产面临的形势不容乐观，必须从经济社会发展全局的高度来重视农业，保证今年农业有个好收成，保持经济平稳较快发展和物价总水平基本稳定。可见，农业收成大局及其对物价的影响，才是温总真正的心头大患。<br />
<br />
虽然西南五省（区、市）的粮产量，仅占全国的16%，旱灾料不会对全年粮产造成致命打击。但受干旱影响，灾区粮价已现涨势，目前云南的晚?稻收购价每公斤已较年初上涨约5％，粳稻较年初涨幅更高达20％。省会昆明的米线每碗亦加价1元，一名店老板的解释是：「旱灾导致大米、油、辣椒、肉等米线的制作原料不断涨价……只好将价格适当上调。」<br />
<br />
CPI「3%以内」目标难达<br />
<br />
温总将今年全年的居民消费价格指数（CPI）上涨目标订在「3%以内」，对此，一向对通胀形势较为乐观的国家统计局总经济师姚景源日前表示，这是「一个难度比较大的指标」。姚景源对近30年内地的通胀情?总结出3点肇因：一是经济过热， 如1994年CPI是24.1%，固定资产投资增长61%；第二是农业特别是粮食出现问题；第三则货币和信贷的过量发放。他说，要保证以上3种情?不碰到一起，否则中国就躲不过通货膨胀。<br />
<br />
现在，第3条肇因似已具备，第1条也有苗头，2月份的CPI仅为2.7％，但已是金融海啸以来首度出现负利率。因此，中央现在要竭力防止的就是农业收成这第2个肇因再来「凑热闹」。<br />
<br />
更何?，随?缺水人畜逐日增加，已出现灾民向昆明、成都等城市「逃荒」的现象，预料春荒后期即6月份后需救助者更多，直接威胁西南半壁河山的社会稳定，温总能不心焦？<br />
<br />
孙嘉业</div>

							
							
							
							
