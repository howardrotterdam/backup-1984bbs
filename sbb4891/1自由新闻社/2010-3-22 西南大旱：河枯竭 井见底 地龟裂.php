																											<div id="postmessage_527990" class="t_msgfont">　　 根据GBN报告中的资料，历史上曾经有3次这样的突变。<br />
<br />
　　 第一次突变，是发生在距今12700年前的“新仙女木事件”。这次突变开始的几十年里温度下降了约5华氏度，迅速降温过后，随之就是持续了1000多年的冷干天气。<br />
<br />
　　 第二次突变，是发生在8200年前的欧洲和其他一些地区的“严冬事件”。这次突变，使得冰川前进、河水冻结、农业生产急剧下跌。<br />
<br />
　　 第三次突变，是在公元14世纪到19世纪中期，北大西洋地区经历了一次相对寒冷的阶段，通常叫做“小冰期”。它带来了严冬和气候突变，对欧洲的农业、经济和政治产生了深远的影响。格陵兰海岸结冰，阻止了商船驶往格陵兰，并且使得渔民在整个冬天不能捕鱼。农民被迫屠宰那些营养不良的牲畜，鱼、蔬菜和谷物供应不足。据统计，这次突变造成了成千上万人的死亡。<br />
<br />
　　 “GBN报告通过类比的方法认为，现在气候的发展也是全球气温逐年升高，而且升高的趋势和历史上的几次突变比较相似，所以就存在着突变的可能性。”<br />
<br />
　　 罗勇说，对于GBN报告中提到的历史部分，他也进行了细致分析：“这些数据，都是对历史上几次气候突变前气温和海水盐度变化的分析。”罗勇说，从每10年温度升高的数值来看，“新仙女木事件”发生前，温度升高是1～2摄氏度，“严冬事件”发生前是3～5摄氏度，“小冰期”发生前是1～2摄氏度，而现在温度升高才0．6摄氏度，远远低于前几次突变前的温度升高数值。<br />
<br />
　　 从气候突变前的盐度降低来看，“新仙女木事件”发生前，盐度降低了2‰～3‰，“严冬事件”前降低了1．0‰，“小冰期”发生前降低小于0．5‰，而现在盐度降低是0．05‰～0．10‰，也低于前几次突变前的降低数值。<br />
<br />
　　 “通过这些数据的对比，我认为在短期内发生气候突变的可能性不大。”罗勇说。<br />
<br />
　　 GBN报告的另一个科学依据是，在世界各大洋的表层和深层，有一股温（度）盐（度）环流输送带，这个输送带从格陵兰附近的北大西洋开始，因为这里的海水的温度比较低，海水盐度比较大，在重力作用的推动下，这里的海水会下沉，然后在海洋深层向南流经南大洋，最后在北太平洋和北印度洋上翻，变成表层洋流，流回到北大西洋，形成一个封闭的环流。<br />
<br />
　　 “环流经过的地方，带去了海洋上的热量和湿润的气候，所以这些地方降水较多，气温也比较好。”但是GBN的报告指出，随着现在全球气温不断升高，格陵兰岛的冰也在不断融化，越来越多的淡水通过陆地上的河流，汇集到了北大西洋，这样北大西洋的海水盐度不断降低，盐度的降低导致海水失去了这种重力的推动，不再形成环流。“于是，没有了环流，海洋上的热量和湿润的气候不能到达陆地上。全球气候突变，气温不断降低也就成了可能的事实。”</div>

							
							
							
							
