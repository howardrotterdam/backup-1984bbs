<br />
　　 “华北地区降水的增多，对北方生态系统的恢复具有巨大作用。”国家气候中心的吴统文研究员最近做的一项研究，就是利用中国科学院大气物理所自行研制的气候预测模式，模拟了现在温室气体排放条件下华北地区降水变化趋势，“在未来十几年里，华北地区的降水可能会增多，这是一个好的现象”。吴统文说，北方降水的适当增多，无疑会刺激这些地方经济的继续发展进步。<br />
<br />
　　 “降水发生转型，对南水北调工程没有太大影响。”吴统文表示，根据他的研究，降水发生转型后，北方的降雨量也只是相当于当地正常年有所增加，“北方的水资源肯定还是很缺乏的，要保持生态的正常发展，还需要大量水资源”。<br />
<br />
　　 “根据我们的预测，如果降水发生转变，可能北方的降水可以增多20％。”罗勇表示，气候变化的不确定性，决定了对于“南水北调”工程的研究也有很多不确定性，现在还不好肯定地说，降水分布的转变对这个工程会有哪些重大影响。<br />
<br />
