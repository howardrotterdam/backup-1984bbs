																											<div id="postmessage_527989" class="t_msgfont">　　 另一方面，目前气候学家对于气候变化的研究，更多地只是涉及气候变化对自然生态系统和社会经济系统的影响等等，“这份报告提醒我们，气候问题不再是一个简单的科学问题，而是一个关系到国家安全的问题”。因为气候突然变冷，会导致农业产量降低，引起食物短缺；洪水和干旱这些气候极端事件将导致淡水供应和水质降低；冰和风暴将导致战略性化石能源的供应中断。“这些都会直接关系到国家安全。”<br />
<br />
　　 “不管这个报告产生的影响如何，也不管将来气候的实际变化如何，作为一个科学家，我觉得我应该做点什么，至少在这方面应该给国家提供一个科学决策的依据。”<br />
<br />
　　 毫无疑问，GBN的报告，引起了中国科学家们求证的强烈兴趣。<br />
<br />
　　 很快，在一批气候学家的努力下，3月4日，以GBN的报告为出发点，中国气象事业发展战略研究领导小组和中国科学院地学部，联合组织召开了“气候变化应对战略研讨会”。<br />
<br />
　　 在这次会议上，专家们肯定地指出，GBN报告里所引用的科学依据，均是得到绝大多数同行科学家公认的研究成果，但是正像作者在报告中明确指出的，该报告的目的不是预测气候将如何变化，而是描绘出如果我们对气候变化没有做好准备的话，气候变化将对人类社会产生怎样的影响。<br />
<br />
　　 “必须指出的是，GBN报告描述的情景仅是极端的情况，也许发生在某些地区，但不会是全球所有地区，而且事件发生的可能性很小。”<br />
<br />
　　 5月10日～12日，香山科学会议第232次学术讨论会，主题为“气候变化的应对战略”。专家建议应当把气候变化问题上升到国家安全的高度来对待，加强对气候变化与国家安全对策的研究，建立国家应对极端天气、气候事件的机制，并纳入国家重大突发事件的应急反应系统中。<br />
<br />
　　 6月29日～30日，青年科学家论坛在北京科技会堂举行，主题为“气候系统模式发展与气候变化应对战略”。GBN的报告再次成为会上大家关注的焦点之一。<br />
<br />
　　2010年前后发生这种突变的概率几乎为零<br />
<br />
　　 罗勇说，GBN的报告中对于全球气候预测的基本思路是，气候突然变冷、变干、风暴增加，从而给人类造成巨大伤害。<br />
<br />
　　 “根据我的研究，GBN预测的情景发生的可能几乎为零。”<br />
<br />
　　 罗勇打开电脑，指着一连串的表格，向记者解释。<br />
<br />
　　 GBN的报告中，引用了两个很重要的用来支持其预测的科学依据。第一个科学依据，就是通过对格陵兰岛冰芯样本的分析，对历史上气候变化的情景进行了细致回顾。他们发现，在历史上每当气温逐渐升高到一定的数值，不利的天气状况可能会相对突然地增多，在这种情况下，气候很可能发生突变，比如气温突然降低，并且降低的幅度和持续的时间都会很长。</div>

							
							
							
							
