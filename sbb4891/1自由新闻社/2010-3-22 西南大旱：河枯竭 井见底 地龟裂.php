													<h2>温总云南视察旱情 婉拒村民斟水 「今年农业生产不乐观」</h2>
						
						
																											<div id="postmessage_526386" class="t_msgfont">明报网上版 2010-03-22<br />
温总云南视察旱情 婉拒村民斟水 「今年农业生产不乐观」<br />
<br />
【明报专讯】内地西南5省（市、自治区）百年一遇的旱情日趋严重，已有逾5000万人受灾。国务院总理温家宝从上周五（19日）起连续3日到重灾区云南省视察灾情，他表示，由于内地同时面对大旱和大雪，今年农业生产形势不乐观。另一方面，干旱令广东西江来水量锐减四成，「鱼米之乡」的云南变成「黄土高原」，四川省拟再度尝试人工降雨，缓解旱情。<br />
<br />
据官方新华社报道，总理温家宝上周五前往旱灾最严重的云南省曲靖市视察，指导抗旱。在慰问当地村民时，温家宝婉拒户主斟茶倒水的好意说：「我们不能喝你的水，运来一点水不容易。」<br />
<br />
西南大旱 北方华东雨雪低温<br />
<br />
考察期间，温家宝指出，目前全国旱情还在发展，北方地区和东部地区又出现低温寡照和雨雪冻害，农业生产面临的形势不容乐观。他呼吁「必须从经济社会发展全局的高度来重视农业」，「千方百计保证今年农业有个好收成，保持经济平稳较快发展和物价总水平基本稳定」。<br />
<br />
温家宝来到库容160万立方米的陆良县德格海子水库。水库已干涸见底，库底泥土裂开了一条条纵横交错的缝隙，密密麻麻的泥块如梅花桩一般。温总踩?干裂的土块走了100多米，弯腰捡起一个蚌壳，沉默良久。他要求有关部门，一要优先解决民众饮水问题；二要适时补种或改种农作物，争取农产供应不缺；三要严防森林火灾；还要确保灾区的粮油、肉类、蔬菜等市场供应稳定，防止价格出现大幅波动。<br />
<br />
粮产减半 普洱茶涨价30%<br />
<br />
西南大旱已令6500万亩以上的农作物受灾。有「鱼米之乡」之称的云南文山州，在干旱下已成「黄土高原」，不少农田绝收。云南省农业部门预测，全省粮产将减少一半。另外，当地近300万亩茶叶受灾，约5万亩茶树枯死，预料今年新出的普洱茶产品价格将平均上涨30%，部分品牌加幅可高达一倍。而首批250吨从外省调运的蔬菜昨日已抵达昆明，以纾缓当地菜价上涨的压力。<br />
<br />
食水紧缺 灾民两月没洗澡<br />
<br />
在严重缺水下，广西桂林的漓江已变成小溪，河?外露，在漓江行船近50年的船主更表示「从没见过漓江水这么浅」。广东省则预计，在西南干旱影响下，料西江来水量会锐减四成，但相信不会因此影响到东江对港澳供水。<br />
<br />
食水珍贵，灾民洗衣洗澡变成奢侈享受，四川省攀枝花市不少灾民最少两个月未洗过澡。有村民反映，不少年轻农民眼见农田失收，生活艰苦，纷纷离家南下打工。<br />
<br />
据中央气象台预测，今明两日，云南、四川、广西等地旱情仍将持续。而四川省则打算在本周进行人工降雨，希望纾缓旱情。<br />
<br />
明报记者报道</div>

							
							
							
							
