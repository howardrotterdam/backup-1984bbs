中国组<br />
<br />
西南地区百年一遇的干旱，已演变成内地今年首场大灾难。当局的统计显示，西南五省区市迄今受旱灾影响民众超过 5,000万人，其中饮水困难达 1,371万人，经济损失达 190亿元人民币；重灾区云南自去年 9月滴雨未下，预计至 5月仍然无雨。<br />
<br />
