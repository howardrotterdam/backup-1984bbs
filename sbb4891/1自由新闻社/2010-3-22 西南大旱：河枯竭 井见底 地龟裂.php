　　从“南涝北旱”到“南旱北涝”，一个轮回70年<br />
<br />
　　 罗勇的分析，在另外一位科学家那里得到了回应。<br />
<br />
　　 “GBN报告里预测，2010年前后，中国的降水类型将由‘南涝北旱’可能变成‘北涝南旱’，这个结论，我在1年前就得到了。”<br />
<br />
　　 北京大学物理学院大气科学系教授钱维宏在实验室里对记者说。<br />
<br />
　　 从1999年开始，钱维宏从事“近百年来我国北方地区干湿变化规律及预测方法研究”。<br />
<br />
　　 他首先分析了我国1961年～2000年间近500个气象站收集到的有关气温和降水的资料。钱教授发现在1979年左右，我国夏季降水出现了一个明显的变化：1979年以前，我国夏季的降水很明显是北方多而南方少；1979年以后，逐渐转变为了南方多，北方少，也就是现在的“南涝北旱”。<br />
<br />
　　 找到这个分界点的钱维宏，有些兴奋，他把数据的收集和整理从现在开始往前推进了120年。120年的数据摆到钱维宏面前时，很快，规律性的东西再一次出现了。<br />
<br />
　　 钱维宏发现，从1910年～1940年，我国夏季降水是南方多，北方少；从1940年～1980年，我国夏季降水是南方少，北方多；从1980年到现在，我国夏季降水又是南方多，北方少。<br />
<br />
　　 “1910～1940年是30年时间，1940年～1980年是40年，从1910年到1980年是70年时间，也就是说从1910年的夏季降水南方多，到1980年又回到夏季降水南方多的状况，这个周期是70年左右！”<br />
<br />
　　 这个周期是否可信？钱维宏再一次把数据收集的时间，向前推进到了500年。这500年间有限的数据再次印证了这个70年周期的准确性。“过去近千年和近 500年的中国东部旱涝分析，揭示出长江与黄河流域的旱涝变化是相对立，并且还是以70年为周期交替出现。”<br />
<br />
　　 得到这样的规律，钱维宏还是不放心，他又从韩国找来了证据。原来，韩国汉城的定量观测降水是从1777年开始的，从这里收集的200多年的数据来看，东亚地区的降水也刚好有60～70年的周期变化。</div>

							
							
							
							
