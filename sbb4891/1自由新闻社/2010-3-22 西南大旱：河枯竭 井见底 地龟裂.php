【本报讯】中国西南地区的云南、广西、贵州、四川和重庆五个省区市旱情持续恶化，居民因缺水难以生存，灾区开始出现逃难潮，仅云南、广西两地就有近百万人外逃避灾，昆明往外地的火车一票难求。当地有官员不忍见人渴死，主动劝村民外出暂避，总理温家宝亦到云南灾区视察。目前灾区已有牲畜渴死，但当局否认有人因缺水死亡。<br />
