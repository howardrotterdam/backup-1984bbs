													<h2>中国西南罕见干旱天灾还是人祸惹争议</h2>
						
						
																											<div id="postmessage_527910" class="t_msgfont"><img src="http://i892.photobucket.com/albums/ac121/wodetuchuang/00-1.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
中国 更新时间 2010年 3月 22日 星期一 12:08 PM 格林威治标准时间<br />
<br />
［b］中国西南罕见干旱天灾还是人祸惹争议</strong><br />
<br />
记者: 黎堡 | 香港<br />
<br />
<br />
　　<img src="http://i.imagehost.org/0362/0_1.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　中国四川农民查看受灾严重的农田(资料照片)<br />
<br />
　　中国西南大片地区正面临罕见的旱灾，农业生产和人民生活受到严重影响。当地一些居民批评政府短期和长期应对旱灾的措施不足，但是熟悉整个地区的地理和地质专家说，该地区众多的人口和它的地貌特征在很大程度上导致这次旱灾十分严重。<br />
<br />
　　*百年一遇的干旱*<br />
<br />
　　自从去年秋季以来，往年降雨量充沛的中国云南、贵州、广西和四川地区一直缺少雨水。半年来的干旱导致农田大面积干枯，一些地方甚至出现了人道灾难。<br />
<br />
　　在云南，百年一遇的干旱造成3千万亩农作物受灾，受灾人口超过7百万。<br />
<br />
　　在广西，1千1百万亩农田受灾，两百多万人缺乏饮用水。<br />
<br />
　　贵州省政府表示，全省绝大部分县市受到不同程度的旱灾，其中50多个县灾情严重；受灾人口超过1千7百万人，其中5百多万人口得不到充足的饮用水。全省受灾农田达85万公顷，也就是近1千3百万亩，其中农作物绝收面积接近18万公顷。<br />
<br />
　　贵阳市居民申有连说，市区居民的用水仍然得到保障，但周边地区的居民缺乏饮用水，农作物受到严重影响。<br />
<br />
　　申有连说：“贵阳是城区，城区现在供水还是正常的，很少有停水的现象，但是周边的县镇上，电视上也报导了，大家的饮水都成问题了。都是用水车给这些居民和村民送水。农田就很紧张了。油菜都没有结籽。水稻那边，因为一直干旱，直接影响到春耕了。还有玉米也受到影响。”<br />
<br />
　　国务院总理温家宝周末在云南省视察灾情，并敦促各地政府做好抗旱就灾工作，并抓好春耕生产，保证物价平稳。<br />
<br />
　　*粮食和蔬菜大幅度涨价*<br />
<br />
　　云南昆明市宜良县居民胡昕俊说，目前县城居民的饮用水还没有受到影响，但物价已经大幅度上升。<br />
<br />
　　胡昕俊说：“象豌豆、洋芋什么的，比没有水(也就是旱)灾前基本上涨了50%左右。”<br />
<br />
　　*政府应对措施受批评*<br />
<br />
　　广西梧州维权人士莫巨烽也说，现在大米一天一个价，食用油、肉类、鸡蛋、大豆和黄豆都明显涨价，但政府还没有采取措施抑制物价上扬；另一方面，当地受灾农民得到的政府援助十分有限，农民基本靠自己渡过难关，预计春耕生产将面临巨大挑战。<br />
<br />
　　莫巨烽说：“政府按每亩给十块钱的柴油费，补贴农民抽水上岸。他们要去很远的河里面去抽水。抽的水很少，抽一天才够浇一亩地。政府的补贴就这么一点点，远远不够。”<br />
<br />
　　一些民众还批评政府应对旱灾的长期措施不足，包括长期忽视农村地区水利工程的兴建和维修、以及没有有效制止生态环境和山地植被被人为破坏。<br />
<br />
　　今年50多岁的贵阳市退休工人申有连对此深有感触。<br />
<br />
　　申有连说：“这个方面确实破坏很严重。在我们住的附近，有一些残留的树林，有时候，来一个什么工程，大片的树林就被毁掉了。平常日常生活中，也经常看见有人砍树。我们在有生之年，看到绿荫荫的、非常繁茂的森林逐步逐步地稀落。我们日常生活中经常看见，看见了就唉声叹气。我们看见这里少一个颗树，又被砍掉了，过几天又看到少了一颗。政府在这方面是有责任的。它对森林的保护只是停留在口头上，实际上做得非常欠缺。”<br />
<br />
　　云南、贵州、广西和四川地区往年降雨量相当充沛，平均年降雨量超过一千毫米。<br />
<br />
　　*专家：干旱属天灾*<br />
<br />
　　对于批评人士认为中国西南地区这次严重旱灾属于人祸大于天灾，地理地质学家、香港大学教授章典说，这次久旱不雨主要还是一种自然的气候现象，而西南地区的喀斯特地貌意味着人们难以从地表摄取干旱前的降雨，使久旱不雨变成旱灾。<br />
<br />
　　章典说：“云南是不缺水的，贵州也不缺水，从整个的降雨量来讲。实际上，云南、贵州，还有川南、重庆，这一带地方，它一个很大的问题，就是喀斯特地貌。它有水，但不在地面，都在地下，在溶洞里面。在很多地方，你不知道溶洞在哪儿。所以，尽管地下有大量的水，但是你找不到洞，你抽不上来。”<br />
<br />
　　在具有喀斯特地貌的地区，具有溶蚀力的水对地表上可溶性岩石进行溶蚀，在地下形成漏斗、落水洞和渠道。一旦雨水停止，积水也很快漏失，形成地表的干旱。<br />
<br />
　　港大教授章典说，自己年轻时经常去贵州农村为村民寻找水井，深知村民缺水的处境。他说，中国西南地区有太多人居住在喀斯特地貌地区，超越了土地的承载能力，但是他们不可能大规模迁徙到其它地区，因此在遇到百年不遇的干旱气候时就很难躲避干旱带来的灾害。</div>

							
							
							
							
