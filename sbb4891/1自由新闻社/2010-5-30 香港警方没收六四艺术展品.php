													<h2>香港民众暴雨中6.4大游行 抗议警方打压　</h2>
						
						
																											<div id="postmessage_648513" class="t_msgfont"><img src="http://i892.photobucket.com/albums/ac121/wodetuchuang/00-1.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
<strong><font size="5"><font color="DarkRed">港澳</font></font></strong> <font size="4"><font color="DimGray">更新时间 2010年 5月 30日 星期天 01:01 PM 格林威治标准时间</font></font><br />
<br />
<strong><font size="6"><font color="Black">香港民众暴雨中6.4大游行 抗议警方打压</font></font></strong><br />
<br />
<font size="4"><font color="DimGray">记者: 黎堡 | 香港 2010年 5月 30日</font></font><br />
<br />
<br />
　　<img src="http://a.imagehost.org/0334/15_2.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　<font color="Blue">司徒华(持话筒者)谴责香港警方扣押民主女神像</font><br />
<br />
　　香港数以千计的民众冒着滂沱大雨参加纪念六四事件二十一周年示威游行。示威活动领袖称一天前香港警方扣押民主女神像和六四事件浮雕是当局对香港民运最严重的打压。当局称扣押行动合法。<br />
<br />
　　<strong>*“平反六四，还我民主女神像”*</strong><br />
<br />
　　<img src="http://a.imagehost.org/0679/16_4.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　<font color="Blue">民主女神像经过香港闹市区 新造民主女神像仍遭警方扣押</font>&nbsp; &nbsp;<br />
<br />
　　<img src="http://j.imagehost.org/0572/17_9.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　<font color="Blue">民主女神像经过香港闹市区 新造民主女神像仍遭警方扣押</font><br />
<br />
　　星期天，香港市民支援爱国民主运动联合会(支联会)率领民众举行纪念六四事件21周年示威游行活动。他们用广东话和普通话高呼“平反六四，还我民主女神像”等口号，冒着倾盆大雨，从维多利亚公园出发向特区政府总部迈进。示威者一方面要求中国中央政府平反六四，释放所有民运人士，另一方面，谴责香港警方一天前扣押刚刚从美国运抵香港的民主女神塑像和六四事件浮雕。<br />
<br />
　　星期六，支联会在香港繁华的铜锣湾时代广场展出塑像和浮雕时遭到警方的阻挠。警方以组织者没有事先申领临时公众娱乐场所牌照为理由进行清场，并逮捕了十多名试图保卫展品的支联会成员，包括立法会议员李卓人。<br />
<br />
　　<strong>*司徒华：面临最严重打压 支联会不屈服*</strong><br />
<br />
　　在星期天游行队伍出发前，身患癌症的支联会主席司徒华在现场表示，警方的最新举动是六四事件以来对支联会最严重的打压，但支联会不会屈服。<br />
<br />
　　司徒华说：“这种打压我们早就预计在内。不管这种打压有多严重，我们支联会一定会立场坚定，坚持到底。”<br />
<br />
　　一些示威者说，警方的扣押行动激起了更多人冒着大雨参加星期天的示威游行活动，以示对支联会的支持和要求平反六四的决心。<br />
<br />
　　<img src="http://j.imagehost.org/0382/18_5.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　<font color="Blue">立法会议员张文光雨水中为六四遇难者默哀&nbsp;&nbsp;</font>&nbsp; &nbsp;&nbsp; &nbsp;<br />
<br />
　　<img src="http://j.imagehost.org/0275/19_6.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　<font color="Blue">社民连副主席陶君行被雨水淋透</font><br />
<br />
　　<strong>*为了年轻一代 男女老少顶着暴雨游行 *</strong><br />
<br />
　　今年七十七岁的王先生说，香港警方扣押民主女神像的举动使他联想起中国大陆警方的一贯做法。<br />
<br />
　　王先生说：“我们香港的警察会逐渐变成内地公安那样。内地公安不讲法律，香港警察也不讲法律了。”<br />
<br />
　　<img src="http://h.imagehost.org/0681/20_6.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　<font color="Blue">香港民众参加六四21周年示威游行</font><br />
<br />
　　尽管暴雨下个不停，示威群众仍然井然有序地列队离开维多利亚公园，穿过繁华的铜锣湾商业区，向特区政府总部进发。沿途，有许多市民陆续加入了游行队伍，也有商家职员和购物者顿足观看示威游行。早已退休的王先生说，他冒着大雨来游行是为了年轻人能了解和记住六四事件。<br />
<br />
　　王先生说：“如果你看到八九年六四那天晚上，看到共产党那样开枪射杀中国人民，尤其是那些年轻人，你的印象会很深刻。如果我们不出来表示一下，年轻人就不知道我们在做什么。你跟那些年轻人说，有这么一件历史，要他们记住。”<br />
<br />
　　年轻人王女士和丈夫手里各自撑着一把雨伞，中间牵着年龄还不足六岁的儿子，走在游行的队伍中。小朋友穿着雨衣，但是雨水不停地拍打着他的小脸。他不停地眨眼，努力地冒着大雨前进。妈妈说，小儿子自己坚持下大雨也要来游行，父母当然不会拒绝，因为中国的民主化与否将影响儿子那一代的成长。<br />
<br />
　　<img src="http://h.imagehost.org/0985/21_7.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　<font color="Blue">一位年轻人不畏倾盆大雨参加集会</font><br />
<br />
　　王女士说：“ 现在香港与国内各方面的交往都很频繁。很多人鼓励年轻人北上找工作。(我儿子)他长大后会不会北上工作也很难说。国内怎么发展会跟他的工作环境有关。”<br />
<br />
　　<strong>*香港政治环境仍比内地宽松很多*</strong><br />
<br />
　　自从1989年学生民主运动遭到中国当局血腥镇压以来，香港市民每年都举行平反六四的示威游行活动。1997年香港主权回归中国之后，民众仍然享有比中国大陆宽松得多的政治环境。对于支联会发起的年度平反六四示威游行，香港警方采取了批准游行、并为游行队伍安排交通管制的做法。<br />
