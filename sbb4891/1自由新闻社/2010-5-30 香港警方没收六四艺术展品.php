																											<div id="postmessage_647985" class="t_msgfont">六四雕塑被港府没收 作者陈维明震惊来港闯关<br />
旅美华裔雕塑家陈维明以”六四”为主题的大型浮雕，在香港被警方没收，陈维明对此表示震惊。他估计，香港政府承受来自北京的压力，进而强行收走艺术展品。事件发生后，陈维明仍然按照原订行程在5月 31日飞往香港，他能否顺利通关入境，将是测试香港有无言论自由？是否真有”一国两制”的指标。自由亚洲电台记者萧融洛杉矶报导<br />
<br />
陈维明从内华达州接受本台电话专访，说明他前一天深夜连续接到民运伙伴从香港等地打来的越洋电话。他说：”我在(美西时间5月29日)凌晨一点半准备睡觉的时候，接到好多电话，我对他们告诉我的消息感到震惊。我想，港府行动一定是来自中共的压力，至少港府认为中共有这方面的需求(没收展品)，要不然不会贸然采取对抗香港民意的做法。”<br />
<br />
陈维明表示，他以人道关怀作为”六四”浮雕创作动机，质疑香港政府没收艺术展品和拘捕有关人员等举动带有明显政治目的。陈维明说：”我希望港府尽快释放香港支联会成员，并放行我的作品参加在维园举行的’六四’活动。我的作品以艺术型式纪念二十年前的历史事件，如果连这东西都不能展示，那香港又如何做到'政治独立'和'一国两制'呢？”<br />
<br />
事件发生后，陈维明向被拘捕的工作人员表示歉意，也谴责港府作为粗暴无理。他指出：”我为中国有这么一批为民主、为自由和为正义抗争，使香港得到一个相对宽松的环境感到非常欣慰，也为本次事件被捕的十三位人士感到抱歉，因为我的雕塑作品使他们受到屈辱，他们的行动是正义的，为中国人民坚守良知和道德底限。”<br />
<br />
陈维明将按原订计划飞往香港参加六四纪念活动，他强调，此行能否顺利通关入境？就能具体反应香港”一国两制”是实情？或是谎言？他说：”我当然要对我的作品负责，我义不容辞地坚定我要去香港的决心。依眼下情势分析，虽然此行入境香港的希望渺茫，我也要去试一试，可以藉此更加证明香港回归之后是更加开放？还是’一国两制’仅是假的？”<br />
<br />
<br />
<br />
