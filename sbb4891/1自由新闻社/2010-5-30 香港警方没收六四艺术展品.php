													<h2>香港警方没收六四艺术展品</h2>
						
						
																											<div id="postmessage_647342" class="t_msgfont"><img src="http://i892.photobucket.com/albums/ac121/wodetuchuang/00-1.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<strong><font size="5"><font color="DarkRed">中国 </font></font></strong><font size="4"><font color="DimGray">更新时间 2010年 5月 29日 星期六 01:51 PM 格林威治标准时间</font></font><br />
<br />
<strong><font size="6"><font color="Black">香港警方回归后首次没收政治艺术展品</font></font></strong><br />
<br />
<font size="4"><font color="DimGray">记者: 张富杰 | 香港</font></font><br />
<br />
<br />
　　<img src="http://h.imagehost.org/0252/1_89.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　<font color="DarkRed">警方封锁雕塑现场</font><br />
<br />
　　香港发生首次政治艺术展品遭警方强行没收事件，警方还逮捕了几位相关的举办人。这是中国六四事件20多年来，香港当局第一次干预此类政治展出并且还抓了人。<br />
<br />
　　香港市民支援爱国民主运动联合会(支联会)原定星期六中午十二点半在香港岛铜锣湾时代广场，公开展出最近由美国运到的有关民主女神像与天安门屠杀的两件大型艺术浮雕 。该会在此前两小时前开始卸下浮雕，并顺利完成组装工作。不过，在原定记者会十二点半开始前的二十多分钟，有超过五十多名警员到场，与支联会成员推撞，当时下着大雨，场面极度混乱。<br />
<br />
<br />
　　<strong>*展品被收走成员被拘捕*</strong><br />
<br />
　　<img src="http://h.imagehost.org/0822/2_47.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　<font color="Blue">支联会常委粱国华被捕后在警车内挣扎</font><br />
<br />
　　最后，支联会有十名成员被拘捕，包括同时是立法会议员的李卓人、支联会常委梁国华与李耀基。原定公开展出有关六四事件的两件大型艺术浮雕也被警方收走。警方事后撤离清场，并没有对媒体解释其行动理由。介绍浮雕的记者会也被迫取消。<br />
<br />
　　支联会主席司徒华在得悉事件后，与另外两名常委何俊仁与张文光立即赶赴到场。司徒华对事件感到愤怒。他指出，在六四事件二十一周年纪念前夕，该会在公众地方放置艺术品，却受到无理没收，这是香港在回归中国后从来都没有发生过的事情。<br />
<br />
<br />
　　<strong>*司徒华要质问香港特首曾荫权*</strong><br />
<br />
　　<img src="http://a.imagehost.org/0228/3_20.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　<font color="Blue">支联会主席司徒华谴责警方行动</font><br />
<br />
　　司徒华说：“究竟发生了何事？我们也不是第一年在时代广场举行活动。为何突然之间发生了这件事情？我要质问(香港特首)曾荫权，究竟你想干什么？你一方面与各民主派进行对话；另一方面，我认为警察今天的行动，是你直接指使下进行的。”<br />
<br />
　　司徒华表示，香港的言论自由意见表达空间不断收窄，香港市民更应挺身而出，参加六四游行与烛光悼念晚会活动，抗议人权不断受到打压。<br />
<br />
<br />
　　<strong>*何俊仁：不清楚是否受中央指使*</strong><br />
<br />
　　民主党主席、支联会常委何俊仁也表示，该会不清楚香港政府这次没收艺术展品的做法，是否背后受到中国中央政府所指使。但他说，时代广场属于公众空间，不需作任何申请，香港警方这次行动带有政治目的，明显是要打压悼念六四21周年的活动，公然践踏了香港的言论自由。<br />
<br />
　　何俊仁说：“我们不知道，但这些做法肯定是违法行为，打压自由表达。大家知道这里时代广场是公众空间。对任何自由意见的表达应该予以尊重，以往我们多年来多次进行类似的表达，都没有受到这般野蛮与粗暴的对待。”<br />
<br />
<br />
　　<strong>*路过市民：香港之耻*</strong><br />
<br />
　　<img src="http://a.imagehost.org/0535/4_15.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　<font color="DarkRed">警方封锁雕塑现场</font><br />
<br />
　　在警方行动期间，有市民围观看热闹，也有人徒步而过。美国之音记者向两位路过市民颜先生与陈女士询问意见。他们都不赞同香港警方的做法，认为支联会有权在公众空间展示艺术品。颜先生表示，警方的做法是香港的羞耻。<br />
<br />
　　美国之音记者随后分别联络香港警察公共关系科与香港特首联络办公室，并留言表明希望寻求这次事件的官方回应，但到截稿前还未收到回复。<br />
<br />
　　<img src="http://j.imagehost.org/0336/5_4.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　<font color="Blue">民主女神像</font><br />
<br />
　　据支联会的介绍，民主女神像是由旅美华裔雕塑家陈维明为纪念六四19周年而创作的。该女神像曾在2008年在美国首都华盛顿国会山广场首展。<br />
<br />
　　另一个大型浮雕[天安门屠杀]也是由陈维明为纪念六四20周年而创作的。浮雕由三组主要画面组成，他们分别是民主女神像被推倒、青年挡坦克，以及军人殴打学生。</div>

							
							
							
							
