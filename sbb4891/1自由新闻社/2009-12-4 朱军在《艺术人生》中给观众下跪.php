																											<div id="postmessage_355822" class="t_msgfont">朱军和周润发一起向观众行古代跪拜礼？这“感人”的一幕出现在央视《艺术人生》中。节目播完后，网上却出现了不少质疑之声。有网民发帖称：“我们当时就在那里，其实事情远远不止是大家从电视里看到的那样！”他们指出，“事实是，周润发那天把朱军狠狠地玩了一把！”<br />
