朱军周润发在节目中下跪。<br />
<img src="http://img3.cache.netease.com/ent/2009/12/4/200912041528089b60d.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
朱军周润发行古代跪拜礼。<br />
<br />
羊城晚报12月4日报道 朱军和周润发一起向观众行古代跪拜礼？这“感人”的一幕出现在前晚播出的央视《艺术人生》中。节目播完后，网上却出现了不少质疑之声。有网民发帖称：“我们当时就在那里，其实事情远远不止是大家从电视里看到的那样！”他们指出，“事实是，周润发那天把朱军狠狠地玩了一把！”从昨日上午开始，由网友们复述的当期《艺术人生》被剪内容开始在各大网络传播，网民一边倒地支持周润发“玩残”朱军。<br />
记者昨日了解到，《孔子》剧组做客《艺术人生》录制于9月2日影片杀青当天，节目为何要“压”三个月才播出？此外据网友透露，那天节目录制了三个小时，但是《艺术人生》却并没有像惯常那样将节目分成上下两集播出，而是将其删减成了短短的半个多小时。这些似乎都证明了：9月2日的《艺术人生》录制现场，确实发生了一些非同寻常的事。<br />
<br />
【现场发生了什么】<br />
据网友爆料，录制一开始朱军先采访胡玫等主创。发哥没有在后台等着，而是坐在导播间里看朱军怎么主持。朱军当然还是一如既往地深情和煽情，还让鲍德熹当场表态“我永远是中国人”。轮到周润发上台，一开口就跟朱军说：“久仰，久仰！”朱军情绪那个饱满啊，从《满城尽带黄金甲》说到《孔子》，发哥除了应几声“对”就没怎么搭理他。到朱军问及当初胡玫怎么到香港找发哥、两人说了些什么时，状况终于出现了！<br />
<br />
“关灯，两千年前只有油灯！”<br />
据网友们回忆，当时周润发先是慢条斯理地说：“说《孔子》应该搞一点气氛。”然后突然抬头大声说：“小吴，把后面的灯关掉！”“前面的灯也关掉。”朱军措手不及：“怎么又把灯关了？”周润发一本正经地解释：“两千年前，是只有油灯的。”当时朱军的表情已经有点“晕”了。<br />
<br />
“带你下跪磕头感谢观众”<br />
更雷人的一幕出现了―――发哥突然拉着朱军，面对现场观众跪了下来，说这是为了让他感受《孔子》是如何复原2000年前的真实生活的。朱军一脸惶恐，没想到发哥又继续拉着他，以额贴地，实实在在地给观众叩了一个头，说要感谢观众。紧接着，发哥向着朱军跪了下来，给他叩了一个头。正当朱军惊恐莫名，发哥学着朱军惯用的深情语调，极其“诚恳”地表示，因为他喜欢看《艺术人生》，朱军跟他父亲的故事令他非常感动。这下，朱军不得不也以磕头“还礼”。<br />
这一招被网友们评价为“以其人之道还治其人之身”，“谁叫朱军一天到晚在节目里拿自己过世的父亲来煽情”。<br />
<br />
“你这西装哪儿来的？”<br />
接下去的录制中，朱军继续“被主持”。当朱军问周润发，成名了膨不膨胀，“我个人感觉应该会膨胀”。周润发说：“成名后，坐房车，结果很不舒服。后来就坐公车，坐地铁，咦，很舒服。”然后突然话题一转：“你这西装，是你的吗？还是赞助的？”朱军先说西装是他妈做的，周润发拉住他的衣领一翻：“嗯，你这个算是名牌。”朱军很无奈：“这关你什么事？”周润发继续揪着他的衣领不依不饶：“这你必须得回答。”朱军只好说：“赞助的。”<br />
周润发还不放过他：“那你做完节目衣服怎么办,是锁柜子里，还是拿回家接着穿？”朱军说：“拿回家接着穿。”周润l问：“你穿着舒服吗？”朱军只好说：“不舒服，一点不舒服……”当时台下一片笑声，有网友表示，这场面让人联想起《卧虎藏龙》里周润发扮演的李慕白教训章子怡扮演的玉娇龙的场景：“教你一点做人的道理！”<br />
<br />
“早睡早起跟太阳一起作息”<br />
据网友们透露，朱军在现场“脸上红一阵，白一阵，完全乱了阵脚”。几次想问周润发一些“深刻”的问题，都被周润发化为无形。譬如他问周润发如何当好一个优秀的演员，周润发的答案是：“早睡早起，跟太阳一起作息。”之后便大劝年轻人珍惜身体，别“白天睡觉晚上瞎闹”。“到后来，朱军明显急躁了，就对着工作人员发泄：‘这个话筒不行啊！’”有几次实在下不了台，他对着周润发和观众大喊，“这节目做不下去了”、“你饶了我行吗”。<br />
后来，还是《孔子》的导演胡玫跑上台，主动爆料周润发如何每天在家中练习下跪数小时，总算帮朱军解了围。<br />
最后观众献花的时候，所有观众都不约而同地跑向周润发，连旁边的周迅都被“冷落”了。“我看那天朱军都快哭了，这一次是他主持《艺术人生》以来最难忘的经历。”网友说。<br />
<br />
网友贴出的“下跪”实录<br />
【网民怎么看这事】<br />
发哥其实是“善霸”<br />
昨日的各大论坛，网友们一边倒地支持周润发“欺负”朱军：<br />
“内地有些名人自以为自己聪明，其实愚不可及。就像朱军，很多次他采访香港演员，那种居高临下的大师派头都让我想把电视机砸了。”<br />
“发哥这次算是给大家报了仇。李敖曾说自己是善霸，周润发倒真的是‘善霸’―――专欺负讨厌的人！”<br />
“一个是真大侠，一个是跳梁小丑，谁都看得出来，周润发严重看不起朱军。”<br />
“发哥从来不把自己当明星，在他看来，给观众下跪并没有什么，但朱军就不是了，发哥这是在教他把自己的姿态放低一点。”<br />
同时，人们也提出质疑。“既然是采访节目，只要没有违反法律法规的内容，就应该全部播出来，为何要删？如果不知道内情，光看节目，还以为发哥真的很欣赏朱军呢，原来全不是那回事！”<br />
