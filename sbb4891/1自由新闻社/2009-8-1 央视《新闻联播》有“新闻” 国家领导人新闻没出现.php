																											<div id="postmessage_187025" class="t_msgfont">央视《新闻联播》有“新闻”　国家领导人新闻没出现<br />
<br />
（2009-08-01）<br />
<br />
联合早报<br />
&nbsp; &nbsp;<br />
&nbsp;&nbsp;<br />
　　（重庆、北京综合讯）一向被称为中国政治风向标的中央电视台《新闻联播》节目前天在编播方式上进行了调整，25分钟的国内新闻中未出现任何党和国家领导人的动态新闻和会议新闻。<br />
　　据《重庆晚报》报道，前天的《新闻联播》新闻提要未出现党和国家领导人的动态新闻，也没有会议新闻的相关介绍。《新闻联播》头条选择了河北、湖南岳阳两地基层干部主动为自己加码，以推动企业、经济快速发展的内容。在25分钟的国内新闻中，同样也无任何党和国家领导人新闻和会议新闻出现。<br />
<br />
　　头条播完后，《新闻联播》的左下方打出“本台短评”的字样，这一栏目在《新闻联播》中也是最近才出现的。 <br />
<br />
　　前天的节目由李瑞英和郭志坚搭档主持。节目的片头并未出现任何变化。<br />
<br />
　　央视的《朝闻天下》和《新闻30分》也分别在周一和昨天改版。<br />
<br />
　　央视新闻中心对外联络人梁蕊妮回答《重庆晚报》询问时说，《新闻联播》的变化，只是《新闻联播》编播上的创新和调整而已，并无改版一说。 <br />
<br />
　　她并表示，其实《新闻联播》近段时间一直都在尝试变化，以提升节目质量，接下来变化还会继续持续下去。<br />
<br />
　　《新闻联播》改版的消息自上月初主播邢质斌退休后流传甚广。消息称，央视将陆续有一系列改版动作，以“新闻立台”为理念，加大新闻节目含量，特别是直播新闻量。 <br />
<br />
　　《北京晨报》日前报道，央视新闻频道今年酝酿的改版更多是微调，不是像有些消息说的会在某一个时间点进行。一些措施已经在实行，只不过没有特意加以说明，观众没有感觉到。例如在逐渐强化国际新闻报道方面，《新闻联播》的国际新闻已由年初的三至四分钟增加到目前的五至八分钟。<br />
<br />
　　参加央视关于新闻节目改版的研讨会国人民大学新闻学院副院长喻国明对媒体表示，《新闻联播》以往大量报道领导人活动的情况“将减少到很低的程度”，而《新闻联播》舆论监督和批评的内容将会有所增加。</div>

							
							
							
							
