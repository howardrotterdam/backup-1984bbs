胡锦涛指出，当前和今后一个时期，要重点抓好以下几项工作。一是要加快文化体制机制改革创新，按照创新体制、转换机制、面向市场、增强活力的要求，加快经营性文化单位转企改制，稳步推进公益性文化事业单位改革，构建统一开放竞争有序的现代文化市场体系，加快推进文化管理体制改革。二是要加快构建公共文化服务体系，按照体现公益性、基本性、均等性、便利性的要求，坚持政府主导，加大投入力度，推进重点文化惠民工程，加强公共文化基础设施建设，促进基本公共文化服务均等化。三是要加快发展文化产业，认真落实文化产业振兴规划，精心实施重大文化产业项目带动战略，推进文化产业结构调整，培育新的文化业态，提高文化产业规模化、集约化、专业化水平。要精心打造中华民族文化品牌，提高我国文化产业国际竞争力，推动中华文化走向世界。四是要加强对文化产品创作生产的引导，真正从群众需要出发，继承和发扬中华文化优良传统，吸收借鉴世界有益文化成果，推出更多深受群众喜爱、思想性艺术性观赏性相统一的精品力作。要引导广大文化工作者和文化单位自觉践行社会主义核心价值体系，坚持社会主义先进文化前进方向，坚决抵制庸俗、低俗、媚俗之风。<br />
<br />
