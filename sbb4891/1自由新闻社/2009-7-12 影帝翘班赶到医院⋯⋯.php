　　干净整洁的病房里，摆放着温家宝赠送的君子兰，绿意盎然。“您送给我的手写的《牛棚杂忆》很宝贵，字工工整整，非常认真，一丝不苟。”温家宝告诉身着红色中式绸装的季羡林，“我每次来都深得教益，去年咱们谈的‘和谐’，您提出人要自身和谐，我向中央作了反映，中央全会决定里就吸收了您的意见。”<br />
　　看到季羡林精神不错，听说他每天坚持写东西、听人读报纸，而且乐观坚信自己能活到茶寿108岁，温家宝十分高兴。他说：“最近，我看您讲良知、良能。我认为，这是知和行的统一观，也是人的品德和能力的统一，这个思想很深刻。”<br />
　　温家宝说：“我喜欢看您的散文，讲的都是真心话。您说自己一生有两个优点：一是出身贫寒，一生刻苦；二是讲真话。对吧？”<br />
　　“要说真话，不讲假话。假话全不讲，真话不全讲。”<br />
　　“就是不一定把所有的话都说出来，但说出来的话一定是真话。”<br />
　　“我快一百岁了，活这么久值得。因为尽管国家有这样那样不可避免的问题，但现在总的是人和政通、海晏河清。”<br />
　　“我们做的不够，还要多努力，把工作做好。您也要把身体保重得更好，多看些年。”<br />
　　温家宝谈起季老送给他的一篇文章《泰山颂》。温家宝说：“写得很好。文章感人，而且有气势。您大概从小就对泰山很喜欢？”<br />
　　“我的家乡在山东。泰山的精神实际上就是中华民族的精神。”<br />
　　“对，这篇文章实际上就是民族颂。”温家宝表示赞同。<br />
