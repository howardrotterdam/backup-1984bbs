													<h2>21世纪是中国人的世纪吗？？</h2>
						
						
																											<div id="postmessage_159197" class="t_msgfont">几年来，季羡林先生一直鼓吹21世纪是东方文化的世纪，而东方文化的主要代表无疑是中国人所创造的文化，因此，东方文化的世纪也就是中国人的世纪。季先生带头“畅想”、“议论”之后，和者甚多，且调门越拔越高。直到此次东亚金融危机爆发，乐观乃至狂热的情绪才稍稍降了些温。<br />
<br />
　　针对这种议论，茅海建在四十余万字的专著《天朝的崩溃――鸦片战争再研究》结尾处写道：“也有一些黑头发黄皮肤的人宣称，21世纪是中国人的世纪。可是，真正的要害在于中国人应以什么样的姿态进入21世纪？中国人怎样才能赢得这一称号――中国人的世纪？不管历史将作何种选择，我以为，鸦片战争留给我们的首要问题是，中国大陆与西方的差距，比起150多年前鸦片战争时，是扩大了，还是缩小了？”茅海建没有回答自己提出的问题，但是他的怀疑与忧虑，尽在不言之中。<br />
<br />
　　“三十年河东，三十年河西”，作为一种历史的可能性，谁也不能否定。作为炎黄子孙，恐怕无人不乐观其成。但是，一种可能性何时变成现实性，就需要脚踏实地地算算帐，清一清自己的家底，再看看别人的进步程度。150多年来差距到底是大了还是小了，我们在这里从经济、政治、科技几个方面粗粗地算一笔账。<br />
<br />
　　两个世纪以来世界主要国家的经济发展状况，经济合作与发展组织（OECD）组织专家进行了比较精细和全面的定量测算，其研究成果麦迪森著《世界经济二百年回顾》（中文版）已于1997年1月由改革出版社出版。不知何故，其中极为丰富而又宝贵的资料至今鲜为媒体引用。该项测算没有提供1840年的数据，因此我们只能以1820年和1992年的数据作为比较的基础。在GDP 总量（单位百万1990年国际美元）方面：1820年，中国199212，高居世界首位；当时西方最发达的国家英国34829，位居世界第四；居第二、三位的是印度和法国；中国GDP 占世界总量的28.7%，比印、法、英三国共占26.6%还要多。1992年，中国3615603，居世界第二位；美国5675617，跃居世界首位；居第三、四位的是日本和德国；中国GDP 占世界总量的12.9%，既少于美国的20.3%，也少于日、德之和的13.5%，比1820年时的比重下降了15.8个百分点。显然，从经济总量来看，中国的进步比不上别人的进步，在150多年的长时段内比不上美国，在最近50年的中时段内比不上日本。日本GDP 占世界总量的比重，从1950年的2.9%提高到1992年的8.6%，百分点增加了近两倍；同一期间，中国GDP 占世界总量的比重，从1950年的6.25%提高到1992年的12.9%，百分点仅增加了一倍。<br />
<br />
　　再看人均GDP 水平（单位1990年国际美元）：1820年，英国1756，是当时的世界最高水平；中国525，是最高值的29.9%.1992年，美国21558，是当今的世界最高水平；中国大陆3098，是最高值的14.4%；台湾地区11590，是最高值的53.8%.结论不难得出：经过150多年的牺牲、奋斗和努力，台湾地区的人民生活水平与西方的差距缩小了近一倍，中国大陆与西方的差距扩大了一倍多。主要原因在于，在1950年至1992年间，台湾的人均GDP 从922提高到11590，1950―1973和1973―1992两个阶段的每年平均复合增长率均为6.2%，总计增长12.6倍；大陆的人均GDP 从614提高到3098，两个阶段的每年平均复合增长率分别为2.9%和5.2%，总计增长5.0倍。在此期间，大陆与台湾人均GDP 水平的差距由1：1.50扩大到1：3.74.抛开枯燥的数字，我们再来看一个比较容易理解的历史事实。在鸦片战争时代，中国与美国的城市化水平大致相当。150多年后，美国早已完成了城市化，城市人口长期稳定在总人口的百分之七八十，而且出现了反城市化，即人口从城市向郊区的反向流动。而中国的城市化至今还没有完全破题，城市人口只占百分之二十几，下个世纪面临着严峻的城市化难题。尤为严重的是，无论美国还是中国的台湾，都不存在所谓农村问题或农民问题，因为在他们那里农民和市民的生活水平大致相当，不存在城乡之间的几十种制度壁垒和“九天九地”的身份、待遇、收入差距。这方面的差距究竟扩大了多少，显然无须再用精确的数字来衡量。<br />
