著名国学大师季羡林于今晨在北京301医院病逝，享年98岁。季老最后一位弟子钱文忠闻此噩耗，哀痛不已，特别在其博客上贴出《哀告》。 <br />
 <br />
今天，按照原计划，我原本应该在“百家讲坛”开始录制“弟子规”，而且，我实现就知道，今天还有来自于北川中学的10位同学和老师前来录制现场，所以，我一早就赶到了录制棚，准备录象。<br />
<br />
但是，一早传来噩耗，恩师季羡林先生于今晨突然逝世。我实在没有办法录制，只能向听众，特别是远道而来的北川同学表示歉意。换上“百家讲坛”为我赶买的黑色服装，赶往301医院附近，和季羡林先生的独子季承先生，以及先生身边的工作人员见面。全国乃至海外媒体都对季羡林先生的突然逝世表示哀悼，我以我个人的名义，并受委托代表季承先生以及先生身边的工作人员，向大家表示由衷的谢意。<br />
<br />
这个消息实在太突然，谁也没有想到。就在前天，我们还和中央电视台的王利芬老师商量拍摄四集祝贺先生百岁华诞的片子，还和有关部门以及季老的家乡落实另外一部记录片的开机仪式，我们都已经做好了分工，以非常欢快和振奋的心情，投入到这些工作中。可是，今天，这一切，所有的这一切只能是纪念和哀悼了。<br />
<br />
最近几个月来，季承先生一直照顾陪伴季羡林先生，老人家的心情非常愉快，胃口很好仍然酷爱吃胡萝卜羊肉饺子;精神也很好，前不久接受王小丫的采访，还思路极其清晰、逻辑极其严密地对着镜头，连续说了半个多小时。他自己多次说，这半年来，他非常愉快。<br />
<br />
就在昨日7月10下午，季羡林先生用毛笔题写了“臧克家故居”，为孔子卫视觉题写了“弘扬国学，世界和谐”，为汶川广济学校题写了“抗震救灾，发扬中国优秀传统”。季承先生在16点到16点30分还为老人按摩了手臂。老人精神依然很好，谈笑风生。<br />
<br />
昨晚，我和季承先生以及老人身边工作人员见面，听到这个消息，还非常高兴。<br />
<br />
岂料，今天一早起来，老人觉得眼皮无力，感觉不好。季承先生于8点左右赶到医院，301医院的医务人员进行了全力的抢救。但是，老人毕竟已经年近百岁，一个小时后，今晨9点，不幸逝世。<br />
<br />
令我们感动的是，北京大学闵维方书记和周其凤校长和其他北大领导很快赶到医院，商讨了治丧事宜。<strong>11点，政治局委员、国务委员，尊敬的刘延东同志也赶到医院;12点，政治局常委、国务院总理，尊敬的温家宝同志也从会场赶到医院。令在场所有的人都无比感动的是，温总理满含真情地讲：“我准备在8月6日为您祝贺生日，还准备和几个问题准备和您讨论啊。”我们对党和国家对季羡林先生的尊敬和关心，深受感动，并深表感谢。对301医院的医护人员表示由衷的谢意。</strong><br />
<br />
季羡林先生漫长人生的最后一段，以我们无法接受的突然结束了。但是，我们知道，老人家是愉快而满足的。<br />
<br />
作为一名学者，老人从来没有停止过思考。他近来正在酝酿提出“大国学”的概念。老人家认为，我们应该用这个概念。“大国学”包括全中华56个民族的文化财富，比如，特别是藏族文化、伊斯兰文化。“大国学”还应包括历代中国人向世界学习的文化成果。<br />
<br />
近来，季羡林先生还高度关注民间办学，他授权一家著名的民办大学筹备了“大国学研究院”，并且建议民办大学也要办人文通识教育中心。原本决定在8月份揭牌，正式成立。<br />
<br />
我们沉浸在毫无准备的哀痛之中，只能先向关心季老的人表示最由衷的感谢。</div>

							
							
							
							
