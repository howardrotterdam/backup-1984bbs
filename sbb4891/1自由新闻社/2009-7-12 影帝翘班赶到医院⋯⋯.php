																											<div id="postmessage_159198" class="t_msgfont">　算完经济账再算政治账。由于对自由、民主、人权等等中国人之间还存在严重分歧，有人认为是普世价值，有人认为是西方价值，尽管已经有学者作过世界各国相关情况的计量、比较和排队，这里暂且不予考虑，我们反思的侧重点先放在国家行政方面。在鸦片战争时代，英国和美国尚未实行文官考任制度。那里的政治家还在把中国的文官制度和科举制度作为行政改革的楷模。1853年，英国议会任命马考莱组织委员会调查东印度公司的职员任用制度，拟成马考莱报告书。在此前后，财政大臣格莱斯顿委派专人调查政府人事行政的状况，拟成屈维廉―诺斯科特报告书，即《关于建立英国常任文官制度的报告》。依据这一报告，英国政府于1855年制定《关于录用王国政府文官的枢密院命令》，揭开了建立英国文官制度的序幕。在美国，直到1883年制定彭德尔顿文官法，才从实行了几十年的政党分肥制过渡到常任文官制。由此可见，在150多年前，中国的文官制度还是领先于西方国家的。此后，清廷在本世纪初废除了传统的科举考试制度。国民政府在大陆时期，文官考试制度一直停留在纸面上。人民共和国到90年代初才制订了公务员条例，正式的法律尚未出台，公务员制度的推行阻力重重，步履蹒跚。1997年香港回归时国人信心十足，一个关键的原因就是港英留下了一支精干的公务员队伍。西方文官系统的效能与廉洁，非眼下中国大陆的文官系统所能企及。进行长时段比较，这方面的形势显然发生了逆转，原来中国的优势反而变成了劣势。<br />
<br />
　　接下来考察地方自治。在鸦片战争时代，中国的官署止于县级，乡级的粮长书手和村级的牌头甲长地方等等，都属于徭役的性质；当时英国的基层地方组织也不是由正式的官员在运行，而是依靠不领薪水的荣誉职乡绅；总之，地方自治均未纳入国家行政体系，均未实现官僚化、专职化。19世纪末，英国通过一系列地方自治立法，确立了基层地方组织的公法人地位，实现了地方自治与地方行政的一体化。在地方自治被地方政府吸纳的过程中出现了以下的关键性转变：自治体的管理者由荣誉性的兼职改为职业性的专职，由无给职改为有给职，由民间精英或者叫乡绅变成了行政官僚。这同时也是权力从有钱阶级向更广泛的阶层和专家转移的过程，即民主化和专业化的过程。而中国到了20世纪末，仍将农村基层组织游离在地方行政之外，搞什么群众自治、村民自治，把类似150多年前英国初级阶段的地方自治视为一种最新的民主创举，这差距实在落得太大了。<br />
<br />
　　在科技差距方面，情况比较令人宽慰。在鸦片战争时代，欧洲已经历了科学革命，产业革命也正在如火如荼地展开。英国牛津大学、剑桥大学建立了几百年，皇家学会也成立了一个多世纪。而在当时的中国，利玛窦带来的《几何原本》及其所代表的古希腊科学成就尚无几人能够领会，近代科学革命的最新成果还完全没有进入中国知识阶层的视野。150多年后的今天，中国已经建立了门类齐全的科技体系，培养了一支庞大的科技队伍，也取得了一批达到世界水平的科技成果。尤其值得一提的是，越来越多的华裔科学家摘取了诺贝尔奖这一科学皇冠上的宝石，他们大多受过中国的科技教育，李政道和杨振宁在获得诺贝尔物理奖时还没有加入美籍，护照仍是中华民国的。显然，中国与西方的科技差距已经缩小，这正是所谓发展中国家的“后发优势”。但是，如果仅仅凭借科学技术这个“第一生产力”，片面地追求“科技兴国”，仍难免走入历史的歧途。<br />
<br />
　　当中国人与现代西方文明刚一接触，有识之士就已指出，西方科技绝非“奇巧无用之物”，而是“船坚炮利”的本源。林则徐、魏源提出了“师夷长技”的主张，洋务运动则把引进现代科技作为中兴、自强的中心环节。然而历史早已证明，科技是现代社会的一个有机组成部分，产业组织不发达，政府体制不改革，科技一花独放、一木擎天是不可能的。<br />
<br />
　　同时应当指出，科学技术体系本身是一种现代社会组织，必须以一种现代精神原则作为运行动力，仅仅依靠增加资金与人员的投入，并不能获得所期待的科技产出。有人作过如下的比较：中国科学院有4万科研人员外加3万辅助人员，1万研究生，一年经费4亿美元，每年被SCI （国际著名的论文检索机构）引证的论文2000篇；德国的马普协会（相当于德国科学院的角色）有2900固定研究人员外加6500流动研究人员，一年经费11亿美元，每年被SCI （国际著名的论文检索机构）引证的论文10000篇。1996年中国从事研究与开发的总人数以及企业研发人员均列世界第4位，而相应指标的国际竞争力只分别列第32位和第21位。中国科技界的种种“教规”严重束缚和压抑了科技人员的积极性，如果没有学术研究和表达的自由空间，选贤举能的竞争环境，尊重和承认个人创造力的保障回馈机制，中国科技赶超世界最高水平就是一句空话。<br />
<br />
　　尤其阻碍中国科技进步的是观念的误导。许多中国人其实并不重视科学，他们重视的只是技术。季羡林先生就说：东西两大文化体系最基本的差异的根源，就在于思维方式的不同。东方主综合，西方主分析。分析出理论，综合出技术。他还十分兴奋地援引波斯人的话说：“希腊人仅懂得理论，唯有中国人才拥有技术。”如果季先生所说的“河东”取代“河西”就是综合技术取代分析理论，这对于中国的科学和技术发展来说，绝对不是一剂补药，而是致命的砒霜。正是由于受类似观点的影响，有关当局已经下手缩减中国的基础科学研究。基础科学是现代应用科学与技术开发的源头，源头被堵塞了，又何来技术革新与技术革命的滔滔洪流，更那里谈得上“科技兴国”。<br />
<br />
　　不论21世纪的中国人多么努力，都难以使中国恢复到18世纪时的世界地位。中国当时正处在“康乾盛世”，人口占世界的三分之一强，GDP 占世界总量的三成；中国的典章制度受到欧陆的主要思想家莱布尼茨和伏尔泰等人的景仰和赞美；中国在周边几十个中小国家建立了朝贡体系，中国皇帝被奉为“天下共主”。在21世纪，中国的人口和GDP 充其量只能达到世界的五分之一，也不可能在任何范围内重建天下体系。因此，与其说21世纪是中国人的世纪，不如说18世纪是中国人的世纪。不论是中国人的世纪，英国人的世纪，还是美国人的世纪均已成为过去，21世纪将是多元一体的人类文明的新纪元。如果中国人不能深刻地反思历史，痛下决心全面改革经济、政治和科技制度，而是陶醉于“半部论语治天下”或热衷于天马行空般畅想，即使不被开除“球籍”，也只能继续徘徊在人类文明的主流之外。<br />
