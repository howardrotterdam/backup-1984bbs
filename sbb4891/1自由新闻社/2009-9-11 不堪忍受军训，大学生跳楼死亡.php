2009-9-10 17:20<br />
<br />
昨日中午，南城南博学院事发宿舍楼前，警方正在勘查。<br />
<br />
如今年轻人的抗挫折能力不高，意志力不够坚强，遇到问题求助无门时，容易产生自杀念头。学校需要更多关注孩子的心理健康。除心理辅导外，学校和社会更要给学生提供一个帮他解决问题的平台，使学生遇到问题时不会觉得求助无门而产生绝望的念头。――― 深圳市社科院院长乐正<br />
　　昨日凌晨，又一个20岁的生命在宿舍楼前飘零。南城南博学院大一男生黄某还没来得及享受大学时光，却因为厌恶才开始了一天的军训，而从宿舍楼上坠落身亡。目前警方已经排除他杀，认为死者符合高空坠落死亡，具体原因正在调查。学校老师向警方反映，黄某性格内向，体态较胖，曾表示不愿意参加军训，老师曾找他谈过心。<br />
　　这是10天内东莞发生的第二起学生坠楼身亡事件。<br />
　　南城南博学院是所全日制民办高校，有上万名学生。前日，学院组织今年新招的3千多名大一新生开始军训。没想到第二天凌晨就发生了惨剧。大一男生黄某因为厌恶军训，从宿舍楼上坠落身亡。<br />
　　死者符合高空坠落死亡<br />
　　昨日凌晨3时许，军训了一天的新生多已进入梦乡，南博学院雅园3号学生公寓7单元楼下，学校保安听到异响，出去查看，发现一名男生躺在宿舍楼下，摸了摸鼻息，男生已经当场死亡。由于09届新生中所有的男生都住在雅园3号学生公寓7单元楼，保安们当场就确定死亡男生是新生。<br />
　　据南城警方透露，死者黄某，广东汕尾人，1989年出生。警方调查发现，死者符合高空坠落死亡，死因正在调查。学校老师向警方反映，黄某性格内向，体态较胖，由于不愿意参加军训，老师曾找黄某谈心。同时，老师也将黄某不愿参加军训的情况向其父母反映，但其父母远在汕尾，便叫亲戚过来找黄某谈心。不想昨日，黄某已身亡。<br />
　　死者曾对军训大发牢骚<br />
　　昨日早上6时许，7单元各院系的新生准备下楼军训，可校方已封锁楼道称，要求学生通过电梯下楼。无奈之余，千余名新生只好乘坐大楼里唯一的电梯下楼。直到上午10时记者到场采访时，很多新生才知道是有人死在了楼梯口。<br />
　　据同为该学院大一新生的知情人透露，黄某身体较胖，前日参加学校一个上午的军训后就疲惫不堪，回到宿舍倒头就睡。另据与黄某同宿舍的一个同学说，黄某讨厌军训，认为军训是军人的事，不是学生的事，对军训大发牢骚。<br />
　　校方称主要因天气太热<br />
　　记者在事发现场看到，11时左右仍有警察在勘查，现场也一直被学校治安员看守着，就连住在该楼的新生都不能进去。一些新生结束军训后，还是不能回到宿舍休息，只能到楼下找阴凉的地方小憩。<br />
　　下午5时左右，记者再次来到事发现场，发现事发宿舍楼楼梯口依然被挡板和警戒线封锁，并有专人看守，学生只能通过电梯上下楼。记者就此事向南博学院进行采访时，该学院派出的一名代表说，目前警方已经介入调查，一切以警方调查为准。至于是不是军训引发惨剧，该学院代表说，军训才开始一天，要说累，也只能是因为天气太热，而不是因军训的强度大<br />
