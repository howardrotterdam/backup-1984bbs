东莞南博学院一新生厌恶军训跳楼坠亡<br />
看到这则新闻又是一阵揪心的痛，为什么现在的孩子遇到问题求助无门时，就容易产生自杀念头呢？为什么那么轻意放弃宝贵的生命呢？<br />
