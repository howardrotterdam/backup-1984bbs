&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; 异端的见证<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;------和一个草根神学家的对话<br />
<br />
郭宇宽<br />
<br />
&nbsp; &nbsp;<br />
<br />
 我是和当地政协和扶贫办的朋友，一起下去考察的，那次的调研，我最初的目地是想了解退耕还林的效果。在昆明的时候，有人告诉我，在云南的深山之中，有不少走火入魔的邪教徒，专门和政府作对。到了武定以后，当地的人告诉我，那里有很多“小众教”信徒，非常顽固，但大多数当地人，都说这些“小众教信徒”也不是坏人，不做什么坏事，就是脑子比较钻牛角尖，有人说他们是“癫子”。这使我产生了很浓厚的兴趣。他们告诉了我，很多那些“癫子”的事迹，乍听来是匪夷所思的，深山之中的一些少数民族村非常之贫困，省长亲自抓,要给当地扶贫,他们都不理会，他们不要发展经济。政府给他们修路，他们不走；政府给他们装自来水，他们不喝，照旧下山挑水。这让我想起了美国的阿米绪人（Amish）。<br />
<br />
 <br />
<br />
&nbsp; &nbsp;清早从武定县城出发，越野吉普行驶了大约3个小时，其中一半以上的时间都没有硬化的土路上颠簸，来到了一个叫做“蚂蟥箐” 的地方，这是一个山顶的苗族村寨，这个地名也让人想到所谓“穷山恶水”，灌溉条件非常恶劣，退耕还林政策显然并没有惠及这里，田地都在山顶上的，只能种些望天收的青稞小麦，甚至连高大的树木都没有，只有不及人高的“爬地松”。颇难想象人为什么会选择这个地方？又是如何在这样的环境中生存下来？联想到“武定 ”的地名，这大概就是历史上在民族战争中，受到镇压，而被驱赶向深山的苗人遗民后裔。<br />
<br />
&nbsp; &nbsp; 在这里我见到了传说中的“小众教领袖”张志民，当地三自爱国会的朋友称他们是“得国派”，当时政府认定的信徒有51个，但实际人数比这要大，这个说法相对于把他们当作邪教是比较善意的，但又含有些嘲弄，意指他们宣称在地上得到了天国。陪同的朋友，并没有把握他愿不愿意和我们交流，因为他以往经常对外面来的干部爱理不理的。<br />
<br />
&nbsp; &nbsp; 我见到他的时候，心中暗吃一惊，他当时正在田里劳作，矮小、黑瘦，看上去饱经风霜，衣服也是破旧的，但却有一股轩昂之气，他把手在衣服上擦了擦，和你握手，礼节是谦卑的，而打量人的目光给人感觉却好像是居高零下。他盯着我看了大概有半分钟，我还没有说什么 话，就问我，你想知道么？他说，你跟我来。 <br />
<br />
&nbsp; &nbsp; 他领着我到了一处山顶的平地，席地坐了下起，陪同他的是村长张志辉，和我一起来的有政府背景的朋友，自觉地坐在了相距7、8米远的地方。张志民不承认自己是宗教领袖，但看得出村民都对他极为尊敬，村长在他面前都像学生一样谦虚，他对自己的信仰也非常的自信。更让我吃惊的是，张志民没有上过学，是个几乎不识字的文盲，写自己的名字都有些困难，他接触圣经，主要靠身边的人读给他听，但他却有过耳不忘的能力，和近乎神奇的理解，能够大段的背诵和引用圣经。<br />
<br />
&nbsp; &nbsp;我们一起聊了大约两个小时，不得不承认，当我和他交流过后，我非但不认为他是一个“癫子”，相反对他有非常的敬意，虽然他没有受过什么教育，但却有着马丁路德那样的情怀，甚至是智性上的天才。<br />
<br />
&nbsp; &nbsp; 后来陪同我的朋友，私下跟我说，他们以前见过张志民多次，但都以为他是个疯老头，以前从来没有听他说过这样有条理的话。<br />
<br />
&nbsp; &nbsp;<br />
<br />
郭：我听说你是自称是基督徒，但不承认我们国家基督教三自爱国会的权威，你为什么会这样？什么时候开始的？<br />
<br />
张：我很小就是基督徒，文革时我们这里的基督徒都受到过很多的镇压，后来可以公开信仰了，从80年代开始，我自己研究历史、天文、地理有一些感悟，后来就在思想上开始和两会拉开距离。<br />
<br />
 <br />
<br />
郭：你的观念和三自爱国会有什么根本的区别么？<br />
<br />
张：我觉得他们的有一些观念很像是迷信，我认为不存在所谓天上的天国，天国是人自己创造的；我认为耶稣是在地上出生的，他和我们一样是人，是有觉悟的人，不是天国派来的。<br />
<br />
而且我不认为圣经讲的都要应验，比如“月亮要不亮，太阳要落下，”我觉得这些不是基督的本意，还有人要复活，这个我觉得也讲不通。假如这么多人都要复活，地球怎么能装得下。<br />
<br />
 <br />
<br />
郭：那你一方面说自己是基督徒，一方面对圣经还有所怀疑，这不是矛盾的么？<br />
<br />
张：我学习圣经是从哲学、科学、精神上理解，我是真正的领会圣经，而那些口口声声一个字一句话背圣经的人根本都不理解圣经。前几年，有一次，三自爱国会的组织我们出去参观，他们也是好心，觉得我这样的住在山里，太闭塞了，外面有许多高楼大厦，确实是我过去没有见过的。我在上海遇见一个神学教授，我觉得很多地方他讲得水平并不高，他告诉我有一个天国比人间最美的国家还要荣耀一百倍，我不公开反驳他，可我知道，虽然他是教授，地位很高，可他说的不对。<br />
<br />
 <br />
<br />
郭：每个人对基督都可以有自己不同的理解，每个人可以有自己内心的上帝，但也用不着和三自爱国会完全分道扬镳啊。<br />
<br />
张：他们觉得我们不守规矩，我们也不想和他们争辩，他们三自爱国会的基督徒要守的立法我不守，唱诗、做礼拜、安息日、圣餐、受洗，我统统不守。<br />
<br />
 <br />
<br />
郭：宗教都是要讲外在仪轨和内在体验的统一，你为什么要对这些宗教仪式，这么抵触呢？<br />
<br />
张：些外表的形式后来变成判断批评别人的标准。这不就成了法利赛人吗？主耶稣是最不喜悦这种虚伪的态度的。礼拜六我要赶集，我不能像他们一样。三自爱国会无用的规矩特别多，还必须要爱政府，拥护党的领导，这不是基督徒的态度。真正的基督徒不是这个样子，非拉铁非教会[1]一样要起义；亚伯拉罕不听他父亲的；大卫不听扫罗的；耶稣不听摩西的；马丁路德不听当时教皇的，基督徒要听的是自己内心的召唤，而不是凯撒的律法，所以我这样的基督徒和他们不一样。<br />
<br />
 <br />
<br />
郭：那你感觉到了自己内心的召唤么？<br />
<br />
张：我感觉到自己受到了一种召唤，是王志民[2]的召唤。<br />
<br />
 <br />
<br />
郭：王志民是谁？<br />
<br />
张：他过去就是我们这里的牧师，最受我们这里贫苦的苗民热爱，他一辈子都在做好事。就因为他49年以后一直拒绝向毛泽东像敬礼，还有拒绝向共产党的干部汇报思想，共产党就迫害他，他并不是说仇恨共产党，但他认为不能逼迫别人去信仰，否则就是邪教。不管别人怎么威胁他，打他，关监狱，他都坚持，不能向偶像崇拜。王志民在文革中被共产党打死了，罪名是诬蔑他曾经带领队伍，打死过七个红军，其实和他根本没有关系，还有他的助手张志清在监狱里被虐待致死，王志民就是为人类的愚昧受难的先知，他是真正的基督徒。现在他复活了，不是他的肉身复活了，而是他的精神在我身上复活了，我感觉到了他的召唤，我就继承他的精神。<br />
<br />
 <br />
<br />
郭：毕竟今天的时代和过去有所不同了，毛泽东时代的那种极端做法，今天即使共产党内部多少也有所反省。我想，基督徒也应该有宽恕精神吧？我听说现在政府来扶贫，你们都不接受，何必呢？这也有些走极端了吧？<br />
<br />
张：可他们没有认罪，还在宣称自己光荣正确伟大，我为了王志民的血债，我内心发誓，共产党只要不认罪，我就不再和共产党合作。<br />
<br />
 <br />
<br />
郭：可你毕竟生活在这个社会，不和政府合作怎么可能呢？<br />
<br />
张：我们拒绝纳税也不要政府来管我们，[3]那时候，政府压迫农民特别厉害，他们从来没有给我们做过什么好事，但我们哪怕杀一头猪，他们都要来收税，和土匪一样。我们就宣布拒绝交税，政府不敢来找我，但是把我们一些兄弟家中的猪牛都牵走了，还拆他们的房子，我们并不反抗，因为我们不喜欢暴力，相信打不解决问题，但我们就是不向这个政府纳税，我们和他们讲道理，他们不听，我们只有忍耐和祈祷。<br />
<br />
 <br />
<br />
郭：这样迟早要爆发大冲突吧。<br />
<br />
张：倒也没有什么大冲突，我们也不希望冲突，有一次影响比较大一点，是92年，我的儿子上山砍柴，结果乡长书记带人当着众人的面，把我的马车烧掉了，我们就到省政府门口静坐，他们要是抓我们的人，我们就一起去坐牢，最后政府知道理亏，赔了一辆马车给我。<br />
<br />
 <br />
<br />
郭：那为什么政府来扶贫，你们也不接受呢？帮你们改善生活总不是坏事吧？<br />
<br />
张：我们是人间天国的子民，兄弟姐妹相互关爱，勤劳生活，我们虽然没有住在高楼大厦里，可我们享受良心的平安和自由，我们不要受政府的役使，也不要政府的任何施舍。（当地以此称之为“得国派”）<br />
<br />
 <br />
<br />
郭：听说你们还不认同计划生育政策，我觉得在你们这样的贫瘠的土地上还是有意义的。<br />
<br />
张：我们不承认共产党的法律，我们只遵照自己的良心行事，原来我确实反对过计划生育，因为孩子是上天赐予的，不过现在我认识到人太多不好，否则我们的土地就不够了，这是我们兄弟姐妹的自觉意愿，但这和法律没有任何关系。<br />
<br />
 <br />
<br />
郭：我听有三自爱国会的人把你们称作“小众教”，我觉得这也还是比较善意的。<br />
<br />
张：三自爱国会因为有政府支持，所以他们人多，就把我们称作“小众教”？这也太荒唐了。在神的眼中只有真理和谬误的区别，没有大众和小众的区别。甚至有的官员说我们是邪教，他们自己才是邪教呢。<br />
<br />
 <br />
<br />
郭：你觉得你自己在这些乡民中间，算不算一个宗教领袖？<br />
<br />
张：我不在乎是不是什么宗教领袖，我只为了自己良心的平安和自由，我愿意说我是一个真正的基督徒。<br />
<br />
 <br />
<br />
郭：为什么要强调自己是真正的基督徒？<br />
<br />
张：很多人恨我，千方百计地说我是疯子，说我讲的不是基督教，让老百姓都不要和我接触。但我知道我在做什么，耶稣说“你们要为我之名，被万民恨恶”，我愿意承受打击，所以我才是真正的基督徒。<br />
<br />
 <br />
<br />
郭：现在信你的人多么？<br />
<br />
张：多少不是问题，耶稣也只有十几个门徒，我最多的时候有上千名信徒，现在在政府的打击和威胁下，很多人害怕都退出了，我们也从不劝阻，因为每个人的内心都属于他自己，我们热爱的就是内心的平安和自由，这是我们选择的。<br />
<br />
 <br />
<br />
郭：以后会不会信你的人越来越少？<br />
<br />
张：多少都不要紧，不见得那些在政府办的大教堂里聚会的人多，就能认识主，因为耶稣说“无论在哪里,有两,三个人奉我的名聚会,那里就有我在他们中间。”<br />
<br />
<br />
<br />
<br />
