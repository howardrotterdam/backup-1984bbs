<br />
在文革中坚持传教，受到政府的嫉恨，最后被扣上煽动群众枪杀红军的帽子枪毙，其实那是当年的苗民抗日团干的，<br />
<br />
阻击了一个排的红军催粮队，后来起义这些人都进了政协，王志民和此事毫无关系，却被杀。”笔者向当地信教群众<br />
<br />
了解，他们对王志民非常同情怀念。国内资料对王志民几乎没有任何记载，而查询Wang Zhiming,英文资料却颇多，<br />
<br />
他是基督教历史被称作圣徒的人物。他1972年在万人公审大会上被杀，1998国圣公会为表彰二十世纪人类的正义奉献<br />
