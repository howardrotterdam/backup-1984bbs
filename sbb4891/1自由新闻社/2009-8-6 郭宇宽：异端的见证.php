<br />
世纪，拜占庭时代，土耳其的回教军崛兴，占据全小亚西亚，毁灭所有基督教会，但非拉铁非蒙特恩，巍然独存。数百<br />
<br />
年来，希腊教徒仍然能在该处保持基督徒的崇拜机会。土耳其政府以后改称之为“阿拉・城”（ALA SEHIR），即“神<br />
<br />
的城”。<br />
<br />
