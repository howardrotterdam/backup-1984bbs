													<h2>GFW负责人方滨兴鼓吹网络审查 称谷歌不服可退出中国</h2>
						
						
																											<div id="postmessage_448794" class="t_msgfont">中国工程院院士：美国令网络信息安全自由流动面临危险<br />
<a href="http://news.xinhuanet.com/comments/2010-01/24/content_12861036.htm" target="_blank">http://news.xinhuanet.com/commen ... ontent_12861036.htm</a><br />
2010年01月24日 00:00:03 　来源：新华网<br />
<br />
新华网北京1月22日电（记者窦灏洋刘军）21日，美国国务卿希拉里・克林顿发表题为“网络自由”的演讲，影射中国限制互联网自由。此前，美国搜索引擎公司谷歌声称，因不满中国网络监管制度准备退出中国。针对这些问题，新华网记者独家专访了北京邮电大学校长、中国工程院院士方滨兴，请他介绍当<br />
前中国和世界各国互联网监管的现状及发展趋势。<br />
<br />
网络监管是国际惯例 谷歌不服中国法律可以退出<br />
<br />
&nbsp; &nbsp; 12日，谷歌在其官方博客上表示，可能将关闭ｇｏｏｇｌｅ．ｃｎ站点和在中国的办公室。在这份声明中，谷歌声称其退出的原因之一是不愿意继续审查“谷歌中国”搜索到的结果。<br />
<br />
&nbsp; &nbsp; 对于这一声明，方滨兴院士表示，网络监管是国际惯例，几乎每个国家都会对互联网信息进行审查，这并不是中国的发明，对这一点谷歌是清楚的。方滨兴院士对谷歌以这个理由来退出感到差异，他说谷歌德国就与美国在线德国、雅虎德国一样，根据德国《青少年保护法》相关部门的要求，其搜索引擎在德国搜索到的内容不会显示非法内容（illegal content）。方滨兴院士当场给我们演示通过<a href="http://www.google.de" target="_blank">www.google.de</a>来搜索色情信息，在谷歌的网页上出现了与谷歌中国相同的德文说明，并在进一步说明的超链中给出了英文声明，“你所搜索到的一些ＵRL根据德国监管机构的规定属于非法信息而未予显示（A URL that otherwise would have appeared in response to your search,?was not displayed because that URL was reported as illegal by a German?regulatory body)”,同时，谷歌搜索结果中还另外提供了一个链接，注明“谷歌接收合法的投诉，根据投诉，谷歌可以从搜索结果网页或所保存的网页中删除相关内容（Google has received a legal complaint and submitted it here to the Chilling Effects database, as described in Google's Digital Millennium Copyright Act policy. In response to the complaint, Google may have removed content from a search results page or hosted page.）。”。由此足以说明谷歌已经适应了在世界各国按照当地政府的要求来限制非法信息的扩散，甚至中国政府对谷歌的要求还没有德国政府要求的高，起码中国政府还没有象德国政府那样要求谷歌必须根据网民的投诉来清除有害信息，在这种情况下，谷歌不因更强硬的审查要求声明退出德国市场，反而以审查为理由要退出中国市场，方滨兴感到百思不得其解，给他的感觉象是出于商业或政治目的而故意挑起事端，甚至是有只手在操纵。<br />
<br />
&nbsp; &nbsp; 方滨兴表示，各国都有各国的法律，网络也应该是有主权的，必须接受本国法律法规管制。我们国家在网络监管方面有《全国人大常委会关于维护互联网安全的决定》、《中华人民共和国电信条例》等法律法规，对何为互联网有害信息界定的很清楚，中国是依法对有害信息进行监管和过滤的，谷歌在中国就该象在德国等其他国家一样要按照当地的法律来做事情，“你要不按中国法律来做事情，你就选择退出中国市场。”<br />
<br />
&nbsp; &nbsp; 世界各国网络管理有差异 但在管理的大方向上是一致的<br />
<br />
&nbsp; &nbsp; 据方滨兴介绍，世界各国都有对有害信息的定义，英国的比较有代表性，它把有害信息分为三类：一类是非法信息，指危害国家安全等国家法律明令禁止的信息；一类是有害信息，比如说鼓励或教唆自杀的信息，虽然没有纳入到非法的信息里面，但是它已经是有害信息了；还有一类就是令人厌恶的信息，比如有些色情信息。美国、法国、加拿大、澳大利亚等国都通过立法等形式，将色情、暴力、危害国家安全、煽动种族和宗教仇恨歧视等信息明确定义为有害不良信息。<br />
<br />
&nbsp; &nbsp; 既然有这个定义，对有害信息该怎么处理？方滨兴将其分为五种情况，第一是法律保障，第二是行政监管，第三是行业自律，第四是技术支撑，第五是经济制约。比如，美国的《通讯新闻准则法案》是1996年颁布的，专门针对网络色情。这个法案规定，任何人故意向18岁以下未成年人散布淫秽信息，就得受两年徒刑。<br />
<br />
&nbsp; &nbsp; 法国专门有一个“费勒修正案”，在这个修正案里专门提出，网络信道――就是提供网络信道的服务商――必须向客户提供信息封锁手段，也就是说，用户能够通过服务商提供的手段封锁了有害信息服务商就算尽责了，否则的话，如果用户通过网络信道获取了不该出现的信息，就要被追究刑事责任。<br />
<br />
&nbsp; &nbsp; 澳大利亚1999年出台了一个“广播服务修正案”，在这个修正案里提出要对未成年人有害的内容信息传播进行打击。包括一些教授自杀的信息，他们都要做打击。同时还出台了一个“反种族歧视仇恨言论法”，就是如果在网络出现这种言论也属于非法行为，也要打击。<br />
<br />
&nbsp; &nbsp; 韩国管理得最严，比中国严得多。韩国也是有一系列的法律，像电子商务通讯法里就专门做什么叫不当站点的建立标准，而且专门公布了互联网内容过滤法律，这是2001年7月份做的，要求在全国范围内过滤违法和有害信息，限制色情及令人反感的网站的站点接入。他们在2001年专门通过了一个修订后的“促进利用和通讯网络法案”，在这个法案里规定，由国家信息通信部（简称MIC）来推广和发展过滤软件，这是一个硬性的规定。2005年以后，韩国还有促进信息化基本法案、电信事业法案、信息通讯基本法等法规，在这里面明确规定，传播淫秽信息，通过黑客手段攻击电脑，传播电脑病毒属于非法行为。“韩国严格到什么程度呢？2008年新修改了一个‘青少年保护法’，禁止19岁以下及高中以下学生在晚上 10点以后出入网吧，这在中国都没有做到。”<br />
<br />
&nbsp; &nbsp; 方滨兴认为，与这些国家相比，中国在具体的管理法规上有差异，但在管理的大方向上是一致的，有些部分做得比较好，有些部分做得弱一点。这一方面与中国互联网规模太大有关，另一方面也与社会发展水平、经济和技术实力有关。比如，2007年，澳大利亚总理签署了一个“NetAlert－保护澳大利亚家庭在线”计划，该计划包括教育、家长支持和提供免费的互联网内容过滤系统，目的是通过努力让儿童免受非法和冒犯性材料的侵扰。该计划总拨款1.89亿澳元，其中拿出8480万澳元用于采购过滤软件免费提供给家庭与学校，这相当于4 亿多人民币，中国要拿出这么多钱来购买过滤软件难度就很大，何况中国的家庭数远超过澳大利亚。再比如技术，美国有众多知名的过滤软件，例如Norton Internet Security、AOL Parental Control、Safe Eyes、K9 Web Protection、N2H2、Smart Filter、Websense、8e6、Cybersitter、NetNanny、Content Protect、Safe Families、Kid Rocket、No Worrys等都是在国际社会上有竞争力的美国产品，在澳大利亚NetAlert计划中中标的过滤产品中就有多款是美国的产品。美国政府曾经推出的一种叫 “食肉动物”的软件恐怕是技术水平最高的软件之一。由于过滤产品的技术水平高，误封的少，因此容易受到网民的欢迎。而像新加坡等国家，由于技术水平相对较弱，就只能采取简单的封堵，经管能够被有害信息轻易地绕过，但做封堵的目的就是告诉大家一个态度，有害信息传播是被禁止的。当然由于技术水平限制而导致封堵误伤了一些有用信息，这也是没办法的事情，这就好比民航无法鉴别什么液体是有害的，干脆就什么液体都不允许带上飞机，哪怕是你当面能够试喝的白水，也绝对不允许带上飞机，而乘客显然已经接收了这一严格的实物过滤。<br />
<br />
&nbsp; &nbsp; 美国的主张自相矛盾 做法危及世界网络安全<br />
<br />
&nbsp; &nbsp; 希拉里在21日的演讲中提到，美国的主张就是让互联网信息自由的流动。对此，方滨兴认为美国是自相矛盾。美国联邦和地方有关限制网络信息流动的法律法规有很多，他就知道至少有加利福尼亚、科罗拉多、内华达、路易斯安那等26个美国州制定了相关的地方法案。明确要求公共图书馆、学校、ISP、家庭等必须采取措施，防止未成年人获取淫秽等有害信息，所以你在美国学校、图书馆、甚至用手机都是接触不到有害信息的，这说明在美国自由也是有环境和条件限制的。在美国，儿童色情信息、种族仇恨信息、未经许可的个人隐私信息、网络欺诈信息、恐怖主义信息等都是严格禁止的，都是不允许自由流动。同时，美国是世界上最主要的过滤软件生产国，世界各国封堵信息使用的过滤软件大多数都是美国公司生产的，所以希拉里的说法和美国的做法是矛盾的。<br />
<br />
&nbsp; &nbsp; 方滨兴指出，美国掌握着国际互联网的根服务器，就相当于掌握了全球互联网的命脉。根服务器就像是全球互联网的“114查号台”，如果哪个国家不听美国话，或与美国的价值标准不一致，或利益发生冲突，美国就可能停掉这个国家的域名解析，那么就会导致这个国家无法通过域名来访问网站，其互联网就形同瘫痪一样而无法再被使用。索马里的互联网服务就曾经由于这个原因而瘫痪过，因此，许多国家都认为，由美国一国掌控国际互联网的生杀大权是很危险的。</div>

							
							
							
							
