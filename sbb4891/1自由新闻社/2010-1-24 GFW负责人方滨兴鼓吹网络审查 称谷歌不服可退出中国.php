																											<div id="postmessage_453527" class="t_msgfont">“谷歌事件”的发生，实际上是美国的一种战略转变。过去，美国通过武力来确保其全球霸权地位，但如今美国实力已经因为国际金融危机和几场反恐战争而大为削弱，于是美国把战略重心从军事领域转移到了互联网领域，谷歌便成了美国实施网络霸权的工具。平心而论，一个大型跨国企业怎么会对其所在国的安全和制度进行干预呢？很显然，这不是企业行为，其背后还有其他因素在驱使。<br />
<br />
　　跨国企业进入另外一个国家，最基本的原则就是遵守所在国的法律和制度；同样，一个国家引入外国企业，也有自己的原则，也是遵守本国的法律和制度。这条原则是一条基本底线，双方都不能触碰。<br />
<br />
　　政府对网络实施管理是一件正常的事情，世界各国都是如此，比方说在反色情、反网络犯罪方面。同时，各国也要求网络内容遵守国家法律、保护国家安全。在这方面，谷歌地球就是一例――它对美国很多地方的地理照片都做了模糊处理。一般来说，各国管理网络的主要办法就是监控、过滤和删除等，尤其是一些西方国家，他们口口声声要求别国“言论自由”、“网络自由”，但是对涉及本国政府利益的东西却严格控制，比如谷歌过滤了很多关于美国种族歧视和包含反美言论的帖子。<br />
<br />
　　中国的网络监管完全依据法律，给予网络公司和网民很大的自由度。从数字上说，目前中国国内的博客数量有2亿多，是世界上博客拥有量最多的国家，每天有上亿人在网络上畅所欲言。<br />
<br />
　　<strong>作为网络，我们人民网非常自由，“强国论坛”栏目已经办了多年。我们做网站的原则和底线就一条，那就是遵守国家法律，保持网络健康发展</strong>。只要不触犯法律，什么都可以提，在我们这里言论是完全自由的。比方说，我们针对各级地方政府，设立了不同的“留言板”，仅2009年就有来自全国各地的7000多个问题通过在“留言板”上的留言被反映出来，并最终得到妥善解决。此外，我们还有“议政广场”栏目，所有人都可以在上面提意见、提建议，最终也大都能得到落实和解决。只要有理念作支持，网络就有生命力，就有灵魂。<br />
<br />
　　谷歌公司是一家创新性很强的公司，有着自己独到的发展理念，这是许多公司难以企及的。我相信，只要谷歌能单纯走企业发展的道路，它将会有很好的发展。希望它不要成为霸权工具。<br />
