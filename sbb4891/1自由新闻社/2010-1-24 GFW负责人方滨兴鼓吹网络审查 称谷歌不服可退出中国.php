<br />
&nbsp; &nbsp; 外交部发言人称：中国互联网是开放的，中国也是互联网发展最活跃的国家，到去年底，中国网民达3.84亿，网站达368万个，博客1.8亿。中国宪法保护公民的言论自由，推动互联网的发展是我们的一贯政策。中国有自己的国情和文化传统，中国对互联网坚持依法管理，符合世界通行做法。我们敦促美方尊重事实，停止利用所谓互联网自由问题对中国进行无理指责。<br />
<br />
&nbsp; &nbsp; 这条新闻相继以《中国坚决反对美利用互联网问题进行无理指责》为题被全国各大网站转载，也吸引了广大读者的关注和议论。<br />
<br />
