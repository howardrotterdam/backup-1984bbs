的确，我们的铁路技术出口到一些国家，对于高铁，无论是武广高铁、郑西还是沪宁，每次运营之后都会有人挑刺、找茬，我觉得是不应该的，或许人家本意不是找茬，只是批评，高铁吃不饱，其实我们想想，几年前绿皮车包打天下的时候，我们坐在绿皮车里当时想什么，当时想要是有高档一点的车，没这么多人我也愿意，现在这种车真的出现了，却有另外的担忧，这车怎么坐不满啊？谁说高铁必须满员才是成功呢？谁说非得像绿皮车那样挤的像沙丁鱼一样高铁才算成功呢？其实我在好几个国家坐过类似的铁路，一节车厢有一两个人，有的时候一两个人很不错了。我们看唐骏在他的自传里曾经写到，当然唐骏最近在风暴眼里头，我们也不说了，他曾经写到，他看见一个日本的服务员在一些空的车厢，推一个小车卖东西，推到一个空的车厢里向空的车厢鞠躬，当时给他震撼很大，那节车厢是空的啊。所以需求是多元化的，有的人可能财力有限，我就坐绿皮车或者长途公共汽车就行了，有人觉得动车也就不错了，还有人希望有更多的个人空间，更加舒适一点坐高铁，我觉得谁也别强加给别人，无论怎么样，高铁都是一个时代的进步，都值得赞扬而不是批评。</div>

							
							
							
							
