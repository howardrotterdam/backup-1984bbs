													<h2>团派大员高调纪念姬鹏飞，旨在18大人事布局中抑制江系人马</h2>
						
						
																											<div id="postmessage_466496" class="t_msgfont">博讯记者于明报道/2月2日，中共在北京人民大会堂高调纪念姬鹏飞诞辰100周年，团派出身的政治局常委、共产党二号王储、国务院副总理李克强出席。10年前，因独子姬胜德涉远华案和出卖军事情报面临死刑，姬鹏飞向江泽民求情未遂而服药自杀，成为自高岗之后第二位自杀身亡的中共高层领导。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;有分析指，姬胜德被判重刑的内幕是其身为国家领导人的父亲姬鹏飞没拿江泽民当回事，引发江泽民的报复。姬鹏飞死后，新华社只发了一则简短消息，江泽民没有出席追悼会。更有甚者，在他死后一年半的2001年10月下旬，中共中央就姬鹏飞死亡补发通告，下达到省、部、军一级党委：“今后对有关姬鹏飞生前活动等，不举办公开形式的研究和纪念。” (博讯 boxun.com)<br />
<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 正忙于十八大人事布局的中共中央，为什么现在破了自己定下的规矩，高调纪念对江泽民以死相抗的姬鹏飞，有分析认为，究其原因，答案就是胡锦涛欲在人事布局中借姬鹏飞之力，抑制江系人马。<br />
&nbsp; &nbsp; <br />
<br />
李克强率政治局团派委员出席<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 2月2日，新华社报道“纪念姬鹏飞同志诞辰100周年座谈会2日在北京人民大会堂举行”。报道称“姬鹏飞同志是久经考验的忠诚的共产主义战士，无产阶级革命家，我国外交战线的杰出领导人”。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 值得注意的是，出席这次座谈会的三位政治局委员全部来自团派，新华社和中国政府网都没有报道政治局常委李克强的讲话，但据新华社的报道，团派出身的中共中央政治局委员刘延东在座谈会上称赞“姬鹏飞光明磊落、襟怀坦白、立场坚定。”“姬鹏飞同志的一生，是革命的一生，光辉的一生，是为共产主义理想和党的事业奋斗的一生，是全心全意为人民服务的一生。”刘延东指“纪念姬鹏飞同志，我们要紧密团结在以胡锦涛同志为总书记的党中央周围，与时俱进…”<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 另一位团派出身的政治局委员王兆国也出席了座谈会。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 李克强率众出席姬鹏飞百年诞辰座谈会，也引起了外界的注意。香港明报在评论这一消息时，十分诧异“出席座谈会的最高官员，却不是中央港澳小组组长习近平，而是不分管外交、港澳事务的副总理李克强”，并提问“这纯属偶然的安排，还是高层分工有变，值得观察”。<br />
&nbsp; &nbsp; <br />
<br />
中共体制内第二位自杀身亡的高层领导<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 据维基百科和《炎黄春秋》杂志等文字记载，1999年，姬鹏飞独子姬胜德因涉厦门远华案和出卖军事情报，被控受贿罪、贪污罪及挪用公款等多项罪名，涉及金额两千多万。当时正在北京香山养老的姬鹏飞得知独子罪行足以判死刑后，先后四次写信给江泽民、张万年、迟浩田，请求宽恕姬胜德，免其一死。姬鹏飞还向薄一波、宋任穷、万里、宋平、谷牧、张爱萍等老同志请求协助，向中央政治局陈情。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 2000年1月中旬，中办主任王刚到北京香山姬鹏飞养老处，告知江泽民看了来信，认为姬胜德案情十分严重，王刚透露：死刑是难免的，但能推迟一、二年执行。姬鹏飞气得破口大骂：“凭我和老伴为党为国奋斗近七十年，共产党就不能刀下留情，给我独子留条命。要死，我就死在中南海”！<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 2000年春节前夕，姬鹏飞要求中央让姬胜德春节时回家一聚，被拒绝。姬鹏飞又约了十多名平日来往较近的老同志，节日到他家聚餐，趁此商议如何为儿子免死，但结果这十多人都托词婉拒了到姬府聚餐。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 绝望之下，姬鹏飞在二月八日中午，于书房写了遗嘱后，用红酒吞服了三十多粒安眠药片。后被送往解放军总医院抢救，但已处于临床死亡，10日被宣布死亡，享年91岁。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 就其逝世，官方新华社只发一则简短消息，其追悼会也以低规格举行，江泽民李鹏缺席，朱F基出席，胡锦涛致简短悼词，中央军委、解放军四总部、国防部等单位，都没有送花圈。在姬鹏飞追悼会上，当时的王储胡锦涛向获得特别许可前来参加父亲追悼会的姬胜德握手致哀。姬胜德参加完父亲葬礼后，被关在总参监护所，8月13日用牙刷柄割脉，并吞服70多片安眠药自杀，经抢救幸免于死。姬鹏飞夫人许寒冰要求江泽民准予姬胜德以高血压症为由保外就医被拒，2001年9月14日晚许寒冰吞服安眠药自杀，未遂。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 2001年10月下旬中共中央就姬鹏飞的政治、组织结论发出了补充意见的通告，通告称姬鹏飞是“就其儿子姬胜德的问题，曾向组织提出了不合理、不合法的要求，被拒绝后，做了、讲了一些具有严重错误的事和话，以极其错误的行为，造成原患病症恶化而死亡”。通告还“建议：今后对有关姬鹏飞同志生前活动等，不举办公开形式的研究和纪念”。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 姬胜德，中国人民解放军高级将领，官至解放军前总参情报部副部长，少将军衔，因涉多项罪名，被江泽民怒骂为“军中败类、民族败类，不杀不足以平民愤”。后被解放军军事法庭一审判处死刑，缓期两年执行。其父姬鹏飞自杀身亡后，姬胜德在2002年被终审判决无期徒刑，由于患有严重心脏病，被保外就医，姬胜德在京郊一军队医院治疗。姬胜德案，引发总参情报部出身的国防部美洲大洋洲局长徐俊平大校于2000年12月叛逃美国。 (博讯记者：于明)</div>

							
							
							
							
