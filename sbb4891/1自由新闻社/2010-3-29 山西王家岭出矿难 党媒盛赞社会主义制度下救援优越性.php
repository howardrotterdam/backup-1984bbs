 连日来，不断有家属和工作人员发生肢体冲突，山西省长王君指示，安抚家属情绪，并要求中煤集团书记到达现场，立即启动家属安抚和赔付预案，进行三人一个小组，逐一安抚。赔偿金额到底是多少？矿方只是称将按照国家规定进行赔偿。<br />
<br />
 在新闻发布会上，有记者再次问到下井员工是否经过培训，及在事发前3天有漏水征兆问题，王家岭项目总指挥孙守仁仍没有正面回答，只是称“到时候会真相大白”。<br />
<br />
 此外，被困人数是焦点之一。针对记者公布被困人员详细名单的要求，孙守仁通报了被困人员的籍贯后，在其他工作人员拥护下离开。<br />
<br />
3道关卡防家属防记者<br />
<br />
 山西政府方面对救援现场的控制越来越严格。除连换了3次通行证外，在通往王家岭煤矿的路上，还设有3道关卡防家属防记者。<br />
<br />
 在河津市，一位的士司机告诉记者，昨天的士公司的老总就特意交待，不要载记者去王家岭煤矿。沿路上，交警对往王家岭煤矿方向的车辆一一进行检查，在通往矿上的唯一路口，设置了重兵把守，家属和一般记者（除中央媒体外）一律不准进去。在矿区，设置了警戒线，严格控制无通行证人员靠近。<br />
<br />
 除此外，为家属安排的宾馆也非常偏僻，记者问了多名的士司机，对方均表示都不知道。<br />
<br />
专家：贴着钢管听，没有任何声响<br />
<br />
 湖南被困矿工家属31日陆续赶到河津。李淑云带着3岁的孩子来找爸爸唐爱军还有伯父唐爱春，每次看到电视播放王家岭煤矿新闻时，孩子就会叫道， “爸爸在那里。”这令李淑云非常揪心。据她介绍，祁阳县观音滩镇有3人被困井下，除唐爱军和唐爱春外，还有唐龙洋。<br />
<br />
 不过来自矿方一亲属的消息让她有了点希望，据该亲属称，当日她和工作人员起肢体冲突时，项目部的人告诉她，井下传出有人敲钢管的声音。但在新闻发布会上，发言人称至今井下被困人员仍没有传递出任何信息。“两个耳朵都听疼了。”一位现场抢险专家称，他到井下贴着原来的钢管听，但水下没有任何声响。</div>

							
							
							
							
