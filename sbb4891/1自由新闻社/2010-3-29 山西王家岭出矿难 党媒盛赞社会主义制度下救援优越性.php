													<h2>传山西矿难实际被困人数超过260人</h2>
						
						
																											<div id="postmessage_540706" class="t_msgfont">传山西矿难实际被困人数超过260人<br />
2010-03-31 01:36:13　来源: 中国日报网(北京)　<br />
核心提示：距离山西王家岭煤矿透水事故发生已过去两天，当地政府称仍有153名矿工被困。该煤矿由中煤一建第63工程处承建，据该公司多位员工称，被困人员数量远多于153人，有人听闻该公司一些经理估算被困井下的实际人数超过260人。<br />
<br />
中国日报网3月31日报道&nbsp;&nbsp;英文《中国日报》3月30日报道：2010年3月29日，曾经的世界污染之都临汾下起了终日不停的绵绵细雨。针对周日一起透水事故的救援工作虽仍在全力进行，却收效甚微。<br />
<br />
至周一晚为止，救援人员们仍在安装排水设备，以保证六百多米井下的抽水工作能够持续。地方官员声称，还有153人被困在这里。<br />
<br />
按照惯例，官方不会在未来几日内公布最终的遇难数字。然而，这次悲剧已必将成为2007年8月17日致使181人丧生的山东新泰透水事故以来，我国遇难人数最多的煤矿安全事故。<br />
<br />
“新泰(事故后)抽水抽了一年。”国家安全监管总局的一位女员工对《中国日报》记者表示，在救援开始之前，王家岭煤矿透水事故的抽水工作至少还将持续两天。<br />
<br />
中煤一建第31工程处一位姓王的救援人员在矿下挥洒了大半天的汗水之后，说他“压根不知道”他第63处的矿工同志们被困何处 ――“太深了”。<br />
<br />
王家岭煤矿出事部分在临汾市乡宁县境内。多年来，临汾一直是各级致命矿难和官员瞒报谎报的大本营，而乡宁是临汾的主产煤区。官方表示，周日下午1点40分左右，王家岭煤矿在工人们凿穿了一个井下老窑后，发生透水事故。多年积水喷涌而出，溃入井下。<br />
<br />
该煤矿由中煤一建第63工程处承建，本将提前5个月完成工期，于今年10月投入运营。据抢险指挥部人员介绍，当班下井261人，其中108人升井，其余153人被困井下。<br />
<br />
然而多位中煤一建的地方雇员声称，被困人员数量远多于此。工人胡文杰说，他“浙江老板所承包的四个队只上来了一个队，其余的都还在下边”。<br />
<br />
“一个队有50到70人。这矿上这么大(180平方公里)，有好几十个队呢，你想想得有多少人?”这名29岁的湖北籍电焊工说。<br />
<br />
另一名雇员则表示，她在周日深夜旁听到一些经理估算被困井下的实际人数超过二百六十。<br />
<br />
无论如何，惨剧都已让这个山中小城的居民们黯然神伤。地方官员们透露，被困雇员中有53人是乡宁人。其他人则来自山西其它地区及河北、湖南、贵州等省份。<br />
<br />
国务院副总理张德江、山西省委书记张宝顺、省长王君及安监总局、煤监局领导人均在周日傍晚赶到现场指挥救援工作。这是近年来我国对死亡30人以上的特大安全事故的标准处置程序之一。<br />
<br />
国家主席胡锦涛和国务院总理温家宝都已做出指示，要求地方官员调动一切力量和设备，千方百计抢救井下人员，并严防发生次生事故。<br />
<br />
王家岭矿是山西庞大煤改计划的一部分。中煤一建和华晋焦煤有限责任公司各控该矿的一半股份。全部被困人员均为中煤一建第63工程处的雇工。<br />
<br />
自去年4月起，山西省政府开始推进规模和力度都极其惊人的煤矿兼并重组改革。改革的主要目标是通过整合时常缺乏安全标准的私营中小煤矿，进行彻底的煤炭产业升级，并最大限度地减少煤矿安全事故的发生。<br />
<br />
<br />
近年来，中央政府的相关努力已让煤矿安全事故遇难人数从2005年的近6000人剧减至去年的2631人。而现在，山西的目标是到今年底，将全省的煤炭企业数量从2200家减少到约100家。<br />
<br />
尽管这些努力成就卓著，矿难并未因此减少，而似乎只是换了主角。去年，我国四起特大煤矿安全事故中的三起均发生在国有煤矿。<br />
<br />
临汾在过去五年间换了五位市长。而这五年内，山西也换了四位省长。多数人的去职与矿难或谎报瞒报安全事故有关。<br />
<br />
临汾曾于2006年被美国非政府组织Blacksmith Institute评为世界污染之都。第二年，也是该组织做出这项评选的最后一年，临汾名列第二。<br />
<br />
(本文来源：中国日报网 作者：胡亦南)</div>

							
							
							
							
