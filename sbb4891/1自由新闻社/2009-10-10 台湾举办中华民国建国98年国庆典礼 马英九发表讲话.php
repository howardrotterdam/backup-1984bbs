--------------------------------------------------------------------------------<br />
&nbsp; &nbsp; <br />
中共大事庆祝建政 60年后，中华民国今日亦迎来 98周年的双十节。台湾因水灾停办庆祝活动，与北京隆重其事形成强烈对比。中共前总书记赵紫阳的秘书鲍彤指，中共建政 60年并未令中国人摆脱被专政，惟台湾已体现主权在民理念；只有在和平民主基础上统一，两岸同胞才有幸福。<br />
