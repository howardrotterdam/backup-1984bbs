今天是十月十日，双十节，中华民国国庆。台湾当局鉴于刚过了八八水灾这「举国悲痛」日子，于是取消了所有双十庆祝活动。只由总统马英九在总统府主持纪念仪式。海外的酒会也取消了。这应是 98年来「最小的国庆」，与中共 60年来「最大的国庆」相比，可说连影子都见不到了。香港人大概也忘了今天是双十。全世界 182个国家和地区，有「国庆节」的只有 40多个，而我们中国就占了两个，是光荣还是不幸？其它国家有称「独立日」（美国）、「统一日」（德国）、「宪法日」（丹麦）、「国旗日」（瑞典）；也有的啥都不叫，径称「巴基斯坦日」、「澳大利亚日」等等。历史悠久的国家，通常定那一天为国庆节呢？以法国为例，并不是以公元五世纪建立法兰克王朝时为国庆，而是以 1789年 7月 14日巴黎人民攻占巴士底狱，推翻君主政权的那一天作为国庆节。这一年，国民议会成立，建立法兰西第一共和国。其后又经过雅各布宾派专政、拿破仑称帝、波旁王朝二次复辟、两次革命、两次王朝，然后出现第二共和以至现在的第五共和。尽管经过这么多次的周折，有五个共和国出现，但法国仍以推翻帝制建立第一共和的 7月 14日为国庆节。若按照法国的先例，那么中国的国庆也应以推翻帝制、建立共和的十月十日武昌起义定为国庆节。这不关乎是认同中华民国还是中华人民共和国的问题。正如今天法国以建立共和的日子为国庆也不意味认同第一共和。当然，若以党派利益或继承中国专制时代「争正统」的政治文化来衡量，就无疑要把党的建立政权凌驾于国之建立共和体制之上矣。此外，若认为当前的共和体制只是虚有其表，政治本质仍是专制政权，那就应以二千多年前建立秦帝国时为「国庆节」。国庆节在大多数国家，是人民认同国家的价值观念并自觉参与欢乐的日子。日前被联合国公布为全球最适合人类居住的国家挪威，在 5月 17日这个又称宪法日的国庆节中，你会看到成千上万人穿着民族服装，走上街头，欢度佳节，到处是绚丽多彩、千姿百态的人的海洋、花的海洋、国旗的海洋。一切都体现人民发自内心对祖国、对民族的认同与热爱。不久前读到居港时事评论员郑汉良的一篇文章，记述他早年在纽约生活时，适逢 1976年 7月 4日美国庆祝立国 200周年，「来自欧洲以及世界各国前来祝贺的高桅帆船，几乎填满哈德逊河，船上的白帆夹杂彩旗随风飘扬……岸边平日节奏忙碌的华尔街金融区……游人观赏世界多个民族艺术家在街头表演……。」「纽约是一个由多个少数民族聚居的文化大熔炉……纽约以文化主题作为美国立国 200周年的重点节目，强调族裔和谐共处，恰好点出纽约或甚而是全美国的特色」。甚么是爱国？人民从心里发出来的对国家体制的认同，人民自发走上街头庆祝国庆就是爱国。街上空无一人，而广场上由 20多万人经地狱式训练而列出方阵，或由执政党遴选的「人民」参与欢庆，那是假扮出来做作的爱国。甚么是和谐？纽约街头的不同种族的「和而不同」就是和谐，尊重每一个人的自由和权利以及每一个人的「不同」就是和谐。列方阵，呼口号，竖立 56条有形无实的民族巨柱，强迫人民认同执政者的国家主义观念，只是「同而不和」。十．一「大国庆」是大而壮观，挪威、美国国庆尊重个人，是小而喜悦。<br />
<br />
今年台湾不办双十国庆活动，究竟是为了八八水灾，还是怕与对岸的「大国庆」打对台，而要像小媳妇似地龟缩到总统府去「庆祝」呢？不过，在笔者看来，「大国庆」的耀武扬威不值得骄傲，重视人文价值、能反映人民真正自由与喜悦的「小国庆」才是小而美。因此，台湾实在应举办一些体认个人自由的嘉年华活动，以「和而不同」来与对岸的「同而不和」大阅兵作对照。</div>

							
							
							
							
