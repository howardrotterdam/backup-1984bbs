													<h2>马英九国庆讲话：从未轻忽对岸的军事威胁</h2>
						
						
																											<div id="postmessage_276538" class="t_msgfont">　　<br />
　　萧副总统、吴主席、宋主席、郁主席、五院院长、各位资政、国策顾问、各位首长先进、各位贵宾、各位女士、先生、以及全国同胞们：大家好！<br />
　　<br />
　　今天是中华民国九十八年国庆。早在八月中旬，政府就决定今年国庆日除中枢举行典礼外，其他庆祝活动全部停止。这是因为「八八水灾」有700多位同胞罹难或 失踪，我们感到就像失去自己亲人一样的哀痛，实在没有心情举办国庆活动；只希望让往生者安息，让家园更为安全，让悲剧不再发生。这些都是我们对灾民以及全 国同胞最庄严的承诺。<br />
　　<br />
　　近年全球气候异常变迁，各地大型灾难频传，在亚太地区尤其显著。两个月前台湾就经v一场大自然的震撼教育，我们亲身学到惨痛的教训。我们深刻领悟， 大地是人类安身立命的居所，面对大自然，人类要学习谦卑，建立顺应环境的发展观念，不再与山河争地。今年将是我们国土规划保育工作的转捩点，我们要快通 过「国土计画法」，做到以安全为优先，以生活为中心，以永续为目标，尤其危险地区一定要禁止或严格限制开发。未来灾区重建，必须考量气候变迁因素，并且与 目前推动中的河川综合流域治理及国土规划相结合。在安全无虞的前提下，必须迁村的地区，可能做到「离灾不离村，离村不离乡」，让社区在重建之后，原有的 文化脉络及经济关S得以永续发展。<br />
　　<br />
　　「防灾重於救灾」是「八八水灾」给我们的另一个关键教训。只要防灾做得好，可以大量减少不必要的伤亡与损失。为达成这个目标，政府已著手改革现有的 灾害防救体系与运作，强化中央与地方的协调联M，落实地方政府各单位平日灾防教育与训练，大幅提高民的防灾意识，并将灾害防救列为国军中心任务之一。全 _ 5个作战区在平日就完成灾害防救的兵力部署；灾害发生前就主动在危险地区预置兵力，时杆媸痹救。事实上，在莫拉克U风造成灾害前，就有21个村落的33 位乡长、村长、警员与土石流防灾专员，断然撤离9,100人到安全地区。如果当时没有及时撤离，死伤估计还会增加1,046人。正是这些可敬的防灾英雄高 度的防灾意识与正确的情势判断，挽救了近千条人命。<br />
　　<br />
　　这次救灾过程也让我们看到台湾人的爱心与这块土地旺盛的生命力。灾后政府与各地民、慈善团体，热心积极投入灾区服务，踊跃捐输，捐款总额达178 亿N元；国军出动超过56万人次，警察、消防与工程人员出动超过41万N人次，志工超过14万人次投入救灾行动。他们不畏艰险，不眠不休的抢救安置灾民， 抢通路桥，抢修堤防，疏通河道，清理家园。这段期间，灾区出现了无数的救灾英雄，救出了5万多位灾民。这些英雄有的是士兵，有的是飞行员，有的是警消人 员，有的是村里长，更有的是志工。我们不一定叫得出他们的姓名，公开表扬的更是少数，绝大多数都是无名英雄，他们默默奉献心力，把灾民当成自己的亲人。洪 水固然无情，但台湾强大的内聚力与生命力，却在艰困时期充分显现，大家不分彼此，相互扶持，这就是台湾否极泰来的希望。我们一定要将这股力量，转化为台湾 未来进步的动力。<br />
　　<br />
　　从去年五月执政以来，我们深深感受到全球正步入一个快速变动与充满挑战的年代，而一个国家如何因应挑战，将是兴衰乃至存亡的关键。我们首先遭逢国际 金融海啸，全球许多银行倒闭，股市狂跌，人心惶惶，伴随而来的是出口停滞、订单锐减、经济衰退、失业大增。政府立即沉稳因应，宣布银行存款全额保障，并多 次降息，稳定了金融；推动扩大内需方案，刺激了消费，提振了信心；绑两岸经贸，开放陆客来_，实现三通直航，开创了崭新机会；规划六大新兴a业，促进a 业多元发展，激发了新的成长动力。此外，政府积极推动节能减碳，提升能源使用效率，成效良好，扭转了过去八年的颓势：一年来全国民节省电量45亿度，相 当於_南县市187万居民的全年用电量；二氧化碳排放量也减少了288万公吨，相当於7,784座大安森林公园一年的二氧化碳吸收量。<br />
　　<br />
　　前述这些拔尖筑底的工程，让台湾安然渡过金融海啸的n击，并迈向低碳经济。我国竞争力在「世界经济论坛」(WEF)的排名从去年第17名一口气上升到今年 第12名，进步了5名；也在世界银行「2010年全球经商环境报告」(Doing Business 2010)中从61名上升到46名，进步了15名。经建会的景气灯号已连续三个月呈现黄蓝灯，今年第三季的经济情况又比第二季明显改善，代表景气已脱离谷 底，经济开始回稳。俗话说：「打断手骨颠倒勇」，台湾不但没有被金融海啸击倒，反而比以前更有信心，更有能力应付外来的挑战。<br />
　　<br />
　　几个月前，H1N1新流感在墨西哥流行时，又一次引起全球恐慌，虽然它不像SARS那样致命，我们却不敢掉以轻心。五个月来，政府与民间的防疫工 作，不论是疫苗、药物的时赣牍卫措施的推动，都不输给先进国家；感染率、致死率目前也控制在较低的水剩而国人在配合上的表现，更令人欣慰。秋冬即将来 临，H1N1疫情可能再度升高，政府已有充分时福有信心将疫情降到最低。<br />
　　<br />
　　莫拉克绝对不是最后一个U风，H1N1新流感也绝不会是最后一次疫情。面对未来可能的天灾，我们要有「料敌从宽，御敌从严」的心理时福将每一个U 风当成莫拉克，把每一种传染病疫情当成SARS来防范，损害一定可以大幅减少。最近芭玛U风来袭，这是继莫拉克U风之后第一个入侵台湾的U风，由於我们记 取先前教训，灾前撤离危险地区7,863位居民，防灾的警觉与时福已有相当进步。几十年来，台湾在风雨之中，淬成长；普遍的忧患意识，让这块土地的人 民，不敢懈，因而渡过各种艰难险阻，这是台湾屹立不摇的根本原因。如何进一步普及并提升防灾意识与行动，将是台湾面对的严厉考验。<br />
　　<br />
　　过去一年，两岸关S大幅好转，_海出现和平契机，这是两岸共同努力的结果，不仅符合大多数人民期待，也赢得国际社会普遍的肯定。一年多来，两岸在 「九二共识」的务实基础上，签署了9项协议，从陆客来_观光到两岸三通直航，从食品安全到司法互助，合作范围日益扩大，善意与互信也逐步累积。我们相信未 来包括两岸「金融监理合作备忘录」(MOU)、两岸「经济合作架构协议」（ECFA）在内，还有很多有利於两岸人民福祉的工作，等待我们去开展。可是两岸 间的差距与疑虑，有其v史因素，不可能一步跨越，旦夕消弭。将来两岸关S的和平发展仍需双方抱持信心，正视现实，循序渐进，以扩大互信，求同化异。<br />
　　<br />
　　近来令人感动与鼓舞的是，去年大陆四川地震，台湾各界踊跃捐输救援，其金额与志工数，高居世界之冠。今年台湾「八八水灾」，大陆不遑多让，其物资与捐款，也居世界之冠。两岸人民这种「血浓於水」的民族情感，让我们对未来的两岸关S充满信心与期待。<br />
　　<br />
　　我们也t解，和平发展两岸关S的同时，国内一部分同胞仍有疑虑，担心国家主权与台湾利益受损。我们愿意经由国会与政党等各种管道的对话与沟通，凝聚 全民大陆政策的共识。台湾是一个民主社会，政府处理两岸议题，一定会以维护国家主权与增进人民福祉为最高指导原则，并接受民意与国会的监督，这点英九念兹 在兹，绝对身体力行，确实做到。<br />
　　<br />
　　两岸关S改善，也有助我国的外交，因为世界各国，不论有无邦交，都乐见两岸对立的缓和，大多数也乐於同时跟两岸发展友好关S。一年多来，我们推动 「活路外交」，两岸同时释出善意，终止长期以来争取建交的恶性斗争，我们邦交国关S渐趋稳定，无邦交国互信增强，而台湾的国际地位逐步提升。也因此，不论 是「亚太经济合作组织」（APEC）领袖会议、「政府窆盒定」（GPA）与「世界卫生大会」（WHA）的参与，都有突破性进展。我国在今年五月批准联合 国 1966年的两项人权公约─「公民与政治权利国际公约」与「经济、社会与文化权利国际公约」，也颇受国际人权界好评。在重返联合国体系的策略上，我们从去 年开始调整过去徒劳无功的n撞，而以参与联合国专门机构及其活动为优先目标，实质提升台湾的国际参与及贡献。这些务实可行的新策略，得到国际社会普遍的肯 定与支持，有效提升了台湾的国际形象与地位。<br />
　　<br />
　　活路外交是正派外交、人道外交，更是软实力外交，不仅拓展了台湾的国际空间，也为我们赢得国际社会的尊敬。今年七月，我国在高雄市举办第八届世界运 动会；九月，在_北市举办第二十一届夏季听障奥运会。这两项大型国际赛会，我国都有极为亮丽的表现。不但中华健儿赢得史上空前数量的奖牌，在参赛100多 个国家中分居世界第7及第5；而主办城市的精心策划，中央政府的全力支援，大量志工的热情投入，台湾人民的友善好客，更令国内外人士感动。无怪乎，「国际 世界运动总会」(IWGA)主席朗佛契(Ron Froehlich)先生公开许，高雄世运是有史以来最成功的世运。「国际听障体育总会」(ICSD)主席艾蒙丝(Donalda Ammons)女士也频频称道，这次_北听障奥运会是八十五年来办得最好的一次。此外，英九得以主办国元首身分，在这两项国际运动赛会开幕典礼中宣布大会 开始，这也是v史上的第一次。由此证明，只要我们有实力，走正路，全世界都会为我们竖起大姆指。<br />
　　<br />
　　如今两岸关S虽然大幅改善，我们却从未轻忽对岸的军事威胁。我们积极寻求海峡和平，但绝不会牺牲台湾的国防安全。因此，「防卫固守，有效吓阻」的国 防战略仍是我们的坚持。我们要积极发展一支以募兵制为基础的专业化军队，同时也是一支「小而精，小而强」的国防武力，以捍卫_澎金马的安全。<br />
　　<br />
　　各位先进，回顾中华民国九十八年的v史，令人感触良深。中华民国政府在大陆三十八年，除了北伐后的「黄金十年」外，多数时间战乱频仍，人民生活颠沛 流离，没有充分机会全面实现国父孙中山先生的建国理想。但中华民国政府迁_六十年间，却能落实土地改革，普及全民教育，推动经济发展，建构社福体系，实施 民主宪政。经过六十年的建设，台湾中a阶级茁壮，媒体自由开放，公民社会稳固，环保意识抬头，社区营造与志工运动蓬勃发展。我们一步一步地创造了一种具有 台湾特色的中华文化，这是我们全体国民共同的资a与骄傲。<br />
　　<br />
　　这六十年来，中华民国与台湾的v史与文化早已结合为一。所谓「台湾精神」，不是空泛的口号，而是展现在曾为这块土地打拼奋斗人民身上的价值与风范。 从日据时代记述台湾先民v史的连雅堂，推动民权⒚傻慕渭水，抵抗日人压迫的莫那鲁道，播种台湾文学的赖和，到近代推动土地改革的陈诚，开创经济转型的李 国鼎、孙运v，以及从《自由中国》到《美丽岛》数十年来争取民主人权的志士。百年来，这些人坚持理想，是「台湾精神」的代表人物。这种精神延续到今天，有 像世运、听奥那些奋战不懈的运动选手，有像灾难中捐款救人的志工及慈善团体，有像九月间在情况危急下牺牲自己、抢救全车乘客的两位大客车司机苏胜裕与侯文 田，更有像把台湾文化带到世界的优人神鼓、表演工作坊与小西园掌中戏；而朱铭的雕刻、廖修平的版画及刘国松的创新国画也深受国际人士重视，在世界艺坛子 一席之地。台湾各个角落几乎时时呈现这种典范，成为「台湾精神」的象徵，也在悠久博大的中华文化中，注入了鲜活的台湾特色。<br />
　　<br />
　　「台湾精神」凸显了台湾人的核心价值，丰富了中华文化的深层内涵，也造就了台湾，造就了你我，更将造就我们的后代。我们应该相互疼惜，携手向前，将「台湾精神」发扬光大，传承百世。<br />
　　<br />
　　再过四百多个日子，就是中华民国建国一百年。一百年前革命先烈的呼唤，仍在我们耳边响；一百年前建国先贤的理想，仍在我们眼前闪耀。民有、民治、 民享依然是我们共同的誓言；民族、民权、民生仍是我们奋斗的目标。为了庆祝这个伟大的日子，并告诉我们的子孙，中华民国在狂风暴雨中，永远屹立不摇；在淬 Z磨练后，更为灿烂夺目，英九特别在今天国庆日，正式宣布成立「中华民国建国一百年庆祝活动筹备委员会」，请萧万长副总统担任主任委员，结合政府与民间， 规划一整年的庆祝活动。<br />
　　<br />
　　各位同胞，我们的信心坚定不移，我们的脚步踏实不虚，我们将倾全力推动各项国家建设，为中华民国奠定未来百年盛世的坚实根基。最后，敬祝中华民国国运昌隆！各位嘉宾H家安康！</div>

							
							
							
							
