<br />
中共辉煌 60年是欺骗&nbsp;&nbsp;<br />
<br />
1911年 10月 10日，湖北武昌爆发起义，推翻了清朝帝制，建立了中国历史上第一个共和政体的中华民国，史称辛亥革命。从 1911年至 1949年，中华民国在大陆管治， 1949年后退居台湾，大陆落入了中共之手。两岸分治 60年，成败与否，各见分晓。鲍彤在这篇专为双十节撰写的文章中指出，双十革命使专制制度成为过街老鼠，但中共领导人毛泽东使用枪杆子夺到政权之后，令独裁统治借尸还魂。中共所谓辉煌 60年是隐瞒和欺骗。前 30年在革命的名义下，几千万人饿死斗死。后 30年在稳定名义下，将一群群维权公民宣布为国家敌人。鲍彤指，大陆 13亿人必须在四个坚持（即坚持共产党领导、坚持马列、毛泽东思想、坚持无产阶级专政和社会主义制度）下生活，「公民得到了选举权没有？共和国得到共和制度没有？答案是一片迷茫。」 <br />
