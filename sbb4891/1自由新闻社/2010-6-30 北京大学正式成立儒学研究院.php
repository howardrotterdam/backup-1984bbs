													<h2>北京大学正式成立儒学研究院</h2>
						
						
																											<div id="postmessage_712885" class="t_msgfont">新京报6月30日报道 昨日，北京大学宣布成立儒学研究院。此前，该校先后成立了国学研究院、高等人文研究院和宗教文化研究院。<br />
<br />
建院讨论多年“终成正果”<br />
<br />
在儒学研究院成立大会暨中国经学史研讨会上，北大校长周其凤表示，该研究院是在北大《儒藏》编纂与研究中心、北大中国哲学暨文化研究所和中国哲学教研室的基础上成立的，其院长为83岁高龄的北大资深教授汤一介。<br />
<br />
周其凤表示，北大有重视儒学研究的传统，在儒学研究上有比较深厚的学术基础和人才队伍。<br />
<br />
北大哲学系主任、儒学研究院副院长王博回忆，此前儒学研究院成立的事情已经讨论几年了，但一直没有确定下来。周其凤到任北大校长后看望汤一介时，汤一介提出要成立儒学研究院，周校长当即表示支持，“他对人文学科在北大的地位作用以及对社会的意义，有很深刻的理解。”<br />
<br />
<strong>“儒家思想可用来培训干部”</strong><br />
<br />
汤一介称，西学对中国传统文化的冲击，为中国传统文化的自省提供了机会，“应该在当前研究儒学，要研究发扬什么、抛弃什么、吸收什么，实现儒学从传统走向现代。”<br />
<br />
武汉大学国学院院长郭齐勇还表示，现在不少高校在开展儒商班，都是对企业家的培训，但这不够，他呼吁儒家思想应该参与到加强干部培训的课程中去。<br />
<br />
对话<br />
<br />
“成立儒学研究院不是跟风”<br />
<br />
<strong>儒学研究院副院长王博表示，儒学必须面对西学价值观的挑战</strong><br />
<br />
新京报：国学研究院和儒学研究院会不会有点重合？<br />
<br />
王博：北大的每个研究院都有不同的宗旨，我们成立儒学研究院完全不是跟风。国学研究院基本是以项目为主进行研究，是一个虚体研究机构。而儒学研究院则是把“儒学”作为惟一的宗旨，一方面注重对儒学经典的整理，同时也要对传统儒学思想进行挖掘。<br />
<br />
新京报：这个研究院会不会招收本科生？<br />
<br />
<br />
王博：会在学生自愿的基础上选择优秀本科生来学习。整合的机构原来就招收硕士生和博士生，也有博士后流动站。<br />
<br />
新京报：确定了哪些研究方向？<br />
<br />
王博：要编纂《中国经学史》，属于纵向研究；在横向研究上，会有“儒释道三教关系史”等的研究；第三部分，儒学要在当代社会发挥贡献作用，必须解决儒学与当代中国的关系，包括“儒学与和谐社会”等。<br />
<br />
新京报：为什么特别强调儒学与当代中国的关系？<br />
<br />
王博：儒学要能够解决现实的问题，要在当代保持活力。必须经过西学的检验，必须面对西学价值观的挑战。<br />
<br />
(本文来源：新京报 作者：郭少峰)<br />
