													<h2>滕彪：公盟不死</h2>
						
						
																											<div id="postmessage_177443" class="t_msgfont">源自:<a href="http://www.gongmeng.cn" target="_blank">www.gongmeng.cn</a> &nbsp; &nbsp; &nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; &nbsp; &nbsp; 2009-07-24<br />
 <br />
<br />
作者：滕彪<br />
<br />
　　<br />
<br />
　　一<br />
<br />
　　<br />
<br />
　　7月17日，早晨起来，窗外淅淅沥沥地下起了雨。打开手机看到短信：“民政局正在查抄公盟办公室！”赶紧打一通电话，交待注意事项，研究对策。许志永火速赶往现场。<br />
<br />
　　<br />
<br />
　　华杰大厦楼下多了搬家公司的大汽车，贼眉鼠眼的便衣，有人扛着摄像机，气氛已经不对了。保安问去哪个房间，我没说话就溜了进去。六楼走廊里人头攒动，有人指挥搬桌子搬椅子，忙忙碌碌，乱七八糟。到了办公室，许志永、李方平和张立辉都在，相视无语。桌子上摆着“取缔公盟法律研究中心”的通知书和“没收物品清单”，我们只能眼睁睁地看着五年来辛辛苦苦积累的东西被翻检、装袋、搬出房间，而且似乎要永远明珠投暗。一群没亲眼见过文革抄家的70后、80后，这回也算开了眼界。<br />
<br />
　　<br />
<br />
　　会议室所在的房间被扫荡一空，办公室的全部电脑、电话、传真机、复印机、打印机、研究报告、维权手册都被抄走，只剩下四张桌子、一个书架。一个民政干部从抽屉里搜出一本国内外媒体的名片册，如获至宝，好像那就是通敌的铁证。我们最心痛的是各种材料：诉讼案卷、上访信、网友来函、会议记录，每一张纸都是凝结了我们的心血。一些上访者只能捡菜叶子吃，却花钱复印自己的冤案材料，如今都落入虎口。<br />
<br />
　　<br />
<br />
　　感觉身后林峥在哭泣。“执法者们”正在往编织袋里装毒奶粉受害者的材料。林峥乃是公盟介入毒奶粉诉讼的最大幕后功臣，一直在与受害者家长联系，办理委托，收集材料，解释法律，在石家庄北京之间往来奔波十余次。有一段时间电话不停，同样的话要与各地家长一一重复数百遍，还要受到误解与质疑，工作之艰辛、琐碎外人难以想象，没有极大的耐心绝无可能持续下来。<br />
<br />
　　<br />
<br />
　　我火了，扯着大嗓门冲他们喊起来：“你们看看公盟得到的这些奖杯奖牌，我们做的是良知正义的事情!你们在干什么？！这些毒奶粉的材料也要抄走！不要把责任都推到你们领导那儿，不要把什么责任都推到制度身上，你们得想想那命令到底是什么意思？现在夺走这些资料的你们自己的双手！假如你们的孩子喝了奶粉毒死了或得了结石，你们怎么想！你们怎么办！如果你们的孩子在这，你怎么跟他解释你们的工作？你敢不敢告诉他们公盟在做的事情和你们在做的事情！”<br />
<br />
　　<br />
<br />
　　干活的兰格衬衫眼镜男挂不住了，出去找警察，警察来了，格衬衫眼镜男说我妨碍执行公务。我说我没有，就把刚才说的话重复了一遍。许志永说，“你们抄走所有的东西，说你们一句‘不讲良心’怎么了？”<br />
<br />
　　<br />
<br />
　　不知哪位长官的意思，他们答应留下毒奶粉的材料。我的冲冠一怒，保住两麻袋材料。这也充分说明经过40年法治文明的飞速发展，今天的确比文革抄家进步了。<br />
<br />
　　<br />
<br />
　　在数年的维权实践中，在法庭、公安等场合，我经常做大义凛然状教训执法者；技巧之一就是拎出执法者的孩子。当年英国殖民地政府在香港实行英国司法制度，让华人手按《圣经》宣誓不作伪证；但发现不管用。中国老百姓最敬畏的，不是基督，而是自家的老祖宗。于是，他们就把原来手按《圣经》起誓的程序，改成对着祖宗牌位盟誓。现在老爷们更不信上帝了，不怕良心受煎熬，不怕遗臭万年，不怕不得好死，天不怕地不怕，甚至连祖宗、父母也不怕了。稍微怕一点的，就是自己的孩子；国保国安，省长书记，也知道心疼自己孩子的；都不希望孩子知道自己的坏事丑事。<br />
<br />
　　<br />
<br />
　　据杨慧文律师讲，上午九点半，穿制服或便衣的20多条汉子闯进狭窄的公盟办公室，把两个志愿者小姑娘吓哭了。杨慧文要求他们出示证件，没有证件的被请出去了。折腾到下午两点，抄家结束，朋友们赶来时发现的是狼藉一片。该带走的全带走了，不该带走的也带走了，包括私人的充电器和钥匙。<br />
<br />
　　<br />
<br />
　　二<br />
<br />
　　<br />
<br />
　　公盟就这样完了吗？我们还有很多的计划准备实施，有很多项目正在进行，很多案件还没有结果，很多会议已经发出邀请，很多志愿者已经排好日程，很多访民在等我们援助，很多网友在开展“日捐五毛”。我们还有太多的理想等待实现。<br />
<br />
　　<br />
<br />
　　2003 年孙志刚事件后，我和许志永、俞江频频被媒体曝光，访民、冤案、求助信也源源不断地来到我们面前。他们说，你们三博士连收容遣送制度都能扳倒，还有什么冤案不能昭雪？其实，收容遣送的废除有许多偶然因素，功劳最大的乃是广大网民的正义呼吁；我们三介书生，哪有什么神通？但事情还是要做，成立一个机构的想法呼之欲出。我们三人与志同道合的张星水律师在2003年10月发起成立“阳光宪道社会科学研究中心”，名字稍微有点奇怪，因为“阳光宪政”的名字没有通过审查。但我们对外简称“阳光宪政”，这就是公盟的前身，当时英文名称the Open Constitution Initiative一直沿用下来。<br />
<br />
　　<br />
<br />
　　当时在北大资源西楼的一间办公室，几个志愿者，几台电脑，一部电话，开始了艰难的起步。一年后，民政局说，你们民间机构不能叫什么研究中心之类，注销了。同时出事儿的还有天则等著名民间机构。我们只好在工商部门注册，成立“北京公盟咨询有限责任公司”，简称“公盟”，下设“公盟法律研究中心”。我们相信，通过参与具有制度意义的典型个案，通过参与公益诉讼和基层选举，通过民间对法治前沿问题的调查研究，可以带动公民意识的觉醒，促进公民社会的发育和进步，推动法治的发展和体制的变革。非暴力、理性、建设性，一直是我们的做事原则。为了机构的持续存在，我们不断进行自我约束、“自我审查”，尽量不碰线、不过线，不做激怒当局的事情，我们不碰8乘8，不碰气功大法；也不碰NED和索罗斯，虽然政府和大学能碰，但它不愿意非政府组织碰。我们极少以公盟的名义接受海外媒体采访。有时候我甚至觉得许志永太谨小慎微了，酱紫的公盟的发展太慢了，能够做的事情和帮助的人太有限了。<br />
<br />
　　<br />
<br />
　　但如此谨慎低调、温和理性的公盟，竟遭此毒手，不能不令人唏嘘。记得几年春节前后，受经济危机影响，大厦里不少公司关门了，我们需要增加一间办公室。我和许志永开玩笑说，你看，咱们不但没受影响，还扩大了一倍！<br />
<br />
　　<br />
<br />
　　2008 年我们的决策制度更完善了，黎雄兵律师加盟后，7人小组负责决策，在田奇庄等人的努力下，办公室的工作也更有效了。我们组织法律培训，推动律协选举，介入三鹿奶粉诉讼，密切关注公益事件，成功举办五周年大会。公盟出现在国内媒体的次数也越来越多；《经济观察报》对许志永、我和郭玉闪分别做了个人专访，公盟逐渐具有了社会影响力。尤其是2009年继续援助三聚氰胺奶粉受害者、介入邓玉娇案和绿坝事件后，知道公盟名字的人迅速增加。我们展望未来，踌躇满志，准备大干一场，没想到风正一帆悬的大海之上，却冒出一股致命的歪风邪浪！<br />
<br />
　　<br />
<br />
　　公盟就这样完了吗？<br />
<br />
　　<br />
<br />
　　三<br />
<br />
　　<br />
<br />
　　我个人的心灵史也是与公盟的成长不可分割的。五年多来，值得回忆的东西太多太多，让人感动的事情也太多太多。<br />
<br />
　　<br />
<br />
　　2004年在北大太平洋大厦的承德案研讨会上，承德死刑冤案陈国清的母亲的哭诉，一直印在我脑海里，常常让我揪心。<br />
<br />
　　<br />
<br />
　　办公室在华清嘉园时主办的人大代表论坛系列，常常看到，没有空调的狭窄办公室挤了数十人，倾听真正的人大代表的斗争故事。<br />
<br />
　　<br />
<br />
　　从黑监狱解救出来的访民张小玉等人来到公盟办公室，她说：我太感谢你们了，我被抓进去当晚就生病了，咳嗽，发高烧，他们不给药吃。如果不是你们，我就没命了！<br />
<br />
　　<br />
<br />
　　我和许志永、张亚东去山西洪洞为黑砖窑受害者代理国家赔偿行政诉讼，听奴工陈小军和庞飞虎讲那并不如烟的往事。我们在回程途中还穿过危险的“炸药村”，去调查更危险的黑煤窑。<br />
<br />
　　<br />
<br />
　　一天下午，办公室来了很多访民，黑龙江的赵桂荣向我讲述，她的丈夫邢世库因为上访而被关在哈尔滨市道外区太平区民主乡精神病院，至今已经800多天了。她多次被关黑监狱，多次被哈尔滨市驻京办毒打，她说她已经很久没哭了，眼泪都干了，但见到我又忍不住要哭。我感到她的整个身体都是在泪水中泡过的。我永远无法忘记她那眼神里的悲愤、屈辱、绝望和哀求。<br />
<br />
　　<br />
<br />
　　还有太多的访民，听到公盟的名字找到我们，希望关注他们的案子。我们只能说非常非常抱歉。有一个下午，我们陷入讨论：做的事情越多，找来的访民就越多，我们无力帮助的就越多，而来访的访民多到一定程度，正常的工作就会受影响。我们心痛。<br />
<br />
　　<br />
<br />
　　就在前不久，负责毒奶粉诉讼的林峥接到恐吓电话：我知道你开的什么车，住的什么地方。有人出5万块钱，让我收拾你。电话免提，录音。哪一家毒奶企业雇的？不太专业。我们一屋子律师都忍住笑从头听到尾。从这里似乎可以看出取缔公盟的（黑）社会基础？<br />
<br />
　　<br />
<br />
　　去年首届公盟公民责任奖，经我们投票，韩寒成为三个获奖者之一。他的助理说，他上公盟的网站研究了一天，感慨到，中国竟有这样的机构！<br />
<br />
　　<br />
<br />
　　是啊，一群知其不可为而为之的傻子，一群不断推石头上山的西绪弗斯，一群拿理想当饭吃的博士律师们和不务“正业”的志愿者们。公盟公盟，也许就是中国特色的傻公民的联盟？<br />
<br />
　　<br />
<br />
　　多少次，公盟的援助律师拒绝收费，或者只要1千元或2千元的象征性费用。他们做的是关涉公益、民权的重要案件，收入却是同行里面最少的。<br />
<br />
　　<br />
<br />
　　多少次，许志永博士被驻京办、截访者和黑监狱的看守们打倒在地，而他却一次次爬起来。<br />
<br />
　　<br />
<br />
　　多少次，各地的网民把捐款送到或汇到办公室，5元，10元，100元，500元；他们说，我们想不到别的办法能帮助你们了。<br />
<br />
　　<br />
<br />
　　多少次，志愿者在办公室里收到或寄出堆积如山的材料，竞选宣言、研究报告，委托书，法律建议，信息公开申请书。<br />
<br />
　　<br />
<br />
　　多少次，在例会上，我们讨论热点案件、分析形势、总结教训、安排会议、分配任务、激烈争论、投票表决。<br />
<br />
　　<br />
<br />
　　多少次，公盟的律师、学者、志愿者，在客车上、火车上、飞机上，在法庭里、报社里、大街上，为访民、拆迁户、养狗人、网民、民工子弟、酷刑受害者、无辜孩子、艾滋病人――奔走、呼吁、辩护。<br />
<br />
　　<br />
<br />
　　四<br />
<br />
　　<br />
<br />
　　洗劫之后，我们决定继续原来的工作，该什么做什么。7月20日，星期一。大家都来上班了。各地网友送来了好几台电脑，有些网友正在把电脑邮寄过来；某QQ群的200个网友集资买了一台崭新的电脑送过来。数百访民要来办公室慰问，许志永含笑劝告他们不要过来，免得造成群体性事件，让维稳办操心。<br />
<br />
　　<br />
<br />
　　办公室里只有四张桌子，显得宽敞多了。电话还是通的，网站还能更新，工作当然应该继续。<br />
<br />
　　<br />
<br />
　　听证会申辩意见、行政复议申请、法律意见书、征求顾问的意见。<br />
<br />
　　<br />
<br />
　　北京市顺义区人民法院已经受理公盟代理的三聚氰胺奶粉受害者马某家属的诉状。在公盟网站上更新消息。<br />
<br />
　　<br />
<br />
　　江西抚州公民乐小女2007年在看守所被打死，他的妈妈以前来过公盟，从黑监狱出来之后立即与我们联系。她还不知道公盟出事了。<br />
<br />
　　<br />
<br />
　　公盟的房东和许志永所租房屋的房东受到巨大压力，他们希望公盟立即撤出，希望许志永马上搬家。许志永说：我很抱歉把你们拖入了这样的麻烦。<br />
<br />
　　<br />
<br />
　　保安在楼下拦截访民。一些前来采访的记者也被拦住。<br />
<br />
　　<br />
<br />
　　办公室随时可能被贴上封条。<br />
<br />
　　<br />
<br />
　　银行账号随时可能被冻结。<br />
<br />
　　<br />
<br />
　　许志永也做好了入狱的准备，虽然可能性非常小。不过这是一个神奇的国度。<br />
<br />
　　<br />
<br />
　　“即使明天就是世界末日，今天仍种我的苹果树。”马丁・路德老人家说得多好。<br />
<br />
　　<br />
<br />
　　公盟的遭遇反映了民间组织的生存困境，也是整个中国社会的悲哀。实际上，这也是每个中国公民所面临的艰难选择：良知、责任、人性、尊严、利益；真实、谎言、升华、堕落、我们只能在这个变迁的制度网络之中寻找自己的位置和生命的意义。<br />
<br />
　　<br />
<br />
　　好在清醒的中国人越来越多。<br />
<br />
　　<br />
<br />
　　很多网友要捐款，很多人发短信或邮件表示支持和声援，有些人主动转发公盟的消息，几个税务专家愿意贡献非常专业的意见，维权人士王荔蕻女士和维权律师郭连辉等表示立即申请加入公盟，火线入伙。荔蕻大姐说得多让人感动：“公盟，这是中国的希望所在。许志永，我被你感动，现在我也成了不沉默的少数。但我无怨无悔。许志永，如果你要遭遇牢狱之灾，我请他们加我一个。二十年前我曾经退缩，我曾经决定把自己捏成一个经济动物。二十年后回顾，我觉得我多活的这二十年毫无价值。因此我决定，这一次我不退缩，不。”<br />
<br />
　　<br />
<br />
　　人们用行动表明，公盟不是一个人在战斗！她继承了追求自由法治和公义的一切先驱者的光荣传统，甘地、特蕾莎、奥西茨基、罗莎・帕克斯、斯坦尼斯拉夫&#8226;马科洛夫，在这一刻灵魂附体！<br />
<br />
　　<br />
<br />
　　翟明磊在声援文章中写到：你不能禁绝民间组织，正如你不能禁绝生活。<br />
<br />
　　<br />
<br />
　　行动即是历史，行动即是永恒。公盟已经写入历史。<br />
<br />
　　<br />
<br />
　　的确，公盟不死，也不会慢慢凋零。<br />
<br />
　　<br />
<br />
　　因为爱与公义永存。因为追求自由和法治的精神不死。<br />
<br />
　　<br />
<br />
　　2009年7月22日</div>

							
							
							
							
