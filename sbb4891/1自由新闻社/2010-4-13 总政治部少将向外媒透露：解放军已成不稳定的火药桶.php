													<h2>一个战友写给中央军委首长的一封信（转）</h2>
						
						
																											<div id="postmessage_561022" class="t_msgfont">作者：一个小兵<br />
在写这封信之前，我要事先申明和解释一些东西。因为我只是一个小兵，只是一个简单的生活了十几年，刚刚来到部队不久，却以前一直热爱祖国热爱党热爱人民的普通中学生。在网上我也只是个普通的网友，因为看到了黑砖窑事件，忽然发现网络谈兵一些问题帖子可以公布一些真相而不被封锁；忽然发现，中央领导人很有可能也在看一些网友写的问题帖子而很有可能受到触痛。所以我决定发这个帖子了。<br />
<br />
一：这封信不是检举信，因为我找不出检举信要求的证据，以及提供本人的真实姓名身份等，我害怕找出证据和提供我的身分后，就被领导给打击垮了。 <br />
<br />
二：真诚的恳求网管和在网上监查的中央一些安全部门能够放过我的帖子，放过发帖子的我，因为我实在害怕，但是更加害怕的是：我们部队的战斗力真的已经被贪污腐败给削减了，而我们的国家领导人还在沾沾自喜自己的科技强军带来的伟大胜利，所以，我还是决定勇敢站出来，写信。<br />
<br />
三：真诚的希望和我有共同感触地战友们**们能够勇敢站出来，说出来，把自己所在军区、所在单位的腐败现象说出来，以证明给中央首长们看。我们部队的腐败问题，真的已经是亟待解决而且火烧眉毛了，并不是我一个人危言耸听，毕竟我只是一个小兵，对一些腐败问题，讲述的不如各位老大哥那么明白和详细。 <br />
<br />
四：部队严禁上网，严禁泄露军事机密。在这里我真诚的告诉我爱的祖国和党：如果揭露腐败也算泄露机密，那么我认了。因为我们部队的发展，需要有责任心，有忧患感的新时期军人。 <br />
<br />
相信看过我前面的申明，我就不用再介绍我自己了。在我们祖国百万雄师中，我也只是普通一个基层部队的普通一兵，但是经过我入伍这么长时间，和各个军区的战友、同学、以及各种关系得来的信息，我已经深深感受到了基层部队腐败带给我们的严重影响，甚至已经使我们部队的战斗力削弱了一半（我这个一半的数据，有待首长调查）。<br />
<br />
下面具体从最基层腐败问题说起，当然我揭发的并不能以偏概全，或许还有清正廉明的部队。<br />
<br />
一、兵源的腐败。我们每个入伍战士，其实文化程度都不是入伍要求的那么高，都必须是高中学历，都必须身体健康，必须像歌里唱的那样，18岁，18岁，我当兵到部队.实际情况是，我们大部分都初中没毕业.最小的13岁多一点.有个战士以前还得过什么肝炎，我们之所以能来部队，能经过部队考核批准，下到连队，是因为我们花钱啊，送礼啊，我们的爸爸妈妈得花一两万才能打通关系，给地方部门送钱，给招兵干部送钱，最后我们就来部队了，素质参差不齐，招兵干部每年去各地方招兵，能受很多钱,是非常油水的工作。<br />
<br />
二、分配部队时候的腐败。在新兵连就不提了，有为了怕挨打图舒服给基层训练我们的连长送礼的，讨好他们的，但他们一般还是比较好的，基本不收礼，对我们都很好。但是在新兵训练结束后，要分配部队了，这时候，就得给负责分配的领导送钱了。现在在部队，送钱最管用，为了给自己分一个舒服点的、好发展的单位，都得给领导送几千块钱不等。<br />
<br />
三、士兵也有三六九等。现在在大城市的兵，都是关系户，关系到打各个军区的处长啊，什么长得官，所以来了后，刚下方基层没多久，就通过各种渠道，去学习开车了。因为将来可以给领导开小车是很好的工作，回地方了也用得上，再差点的就送钱给领导，然后去机关当公务员，反正最后分到基层的都是又没钱又没关系的人。即使这样，也有很大一部分人也有一点点关系，用人的话讲，没关系，能到部队来么？反正一句话：现在部队都是关系兵。<br />
<br />
四、炊事班的腐败。这是最关系到基层官兵的要害。这个衙门可不像电视剧里《炊事班的故事》那么每天阳光和幼稚。在现实中，要像胖洪班长那么傻的人，是混不到司务长的。司务长掌管着一个单位的财政和我们的伙食。可是单位领导每年得有很多花销都得从灶上拿钱。请机关参谋干事吃饭得从这里花钱，请其他外单位的也得从这里花钱。有时候自己家没米没面了也得从这里拿。掌管后勤伙食的副主官还要贪点捞点。然后就是司务长黑能每年从这里贪污。那我们也得吃饭啊，怎么办?省呗。从我们官兵的嘴里省。八个人一桌一顿只给三块肉，怎么分啊？结婚了的干部还能回家自己补，我们战士们只好自己买方便面泡了。有个战士天天泡面，都口腔溃疡了。为什么不换司务长？他是我们机关某个领导的老婆的侄子。<br />
<br />
五、套改士官、考学的潜规则。现在可不是靠吃苦能干，就能从战士套士官了。得花钱。套一级得花一两万，一级套二级得两三四万，二级套三级得花三四五万，三级套四级得花四五六万，四级套五级更多，五级套六级花钱也不一定管用，得到军以上机关找领导。士官提干部更得花钱，现在战士提干名额少了，所以花钱更多，估计得花个10万。同样战士考军校，也不是看学习成绩。还得花钱。五六七万保守算。 <br />
<br />
六、干部升官的秘诀。现在的干部，要想当个副连、正连职务。（政工干部更得跳来跳去，找位置）都得给团长、旅长、政委之类的主官送钱。没钱光有能力也不行。现在每个机关的小干事小参谋都是花钱送礼进去的。老**们你们以前还能有基层主官推荐到机关锻炼，现在没这个优良传统。想当个政委，恐怕10万20万下不来吧，不信你问退下来的人。<br />
<br />
七、各种肥缺的发财方法。在空军，各个机场的油料股长，航材股长，主管基建的领导，站长都是捞钱的肥缺。有个说法，宁当油料股长也不当站长（股长归站长管）。油料股长把部队每回飞行完剩下的油用油罐车卖给地方，一年能挣百八十万都是小的（他们还得给上头领导送钱，不然就自找麻烦）。站长就是天天搞建设，今天拆这个楼明天修这个楼，反正每个项目最少得200万，自己从中捞钱。政工干部首长就是卖官，一个指导员标价多少万，一个政工干事多少钱，都有标价的。<br />
<br />
八、上级检察机关下部队的猫腻。每年上级机关来基层部队检查，巧立名目，各种检查项目迎接不暇，已经严重干扰了部队正常的训练工作。来一次，部队得给他们塞一次钱。一个小干事估计2000块差不多。大点的下来就得吃大餐送大礼。不然他们真查我们，查出问题来，领导害怕啊。<br />
<br />
九、安全大检查不见效果的原因。空军摔飞机，一般都得停飞、然后安全大检查，发生严重事故了，中央首长们非常着急，得在找问题，为什么摔飞机解决不了。其实每次我们停飞安全大检查都在检查什么？检查卫生有没有死角，检查被子叠得是不是豆腐块。我们都郁闷死了，你说叠被子和摔飞机有什么联系。<br />
<br />
十、干部转业的无奈和辛酸。干部一般面临着转业。一般情况是：当这个干部年轻力壮时候，想转业，部队不让转，要想转，你得花钱。当这个干部年老力衰想要留部队的时候，部队不让留，想留，你得花钱。所以每年的专业时候，每个部队领导主管都能捞一大笔钱。<br />
<br />
敬爱的中央军委首长们，不知道你们能不能百忙之中抽空看到我写的这些东西，也许它能给你们的管理工作带来一些思路，能够给你们百思不得其解的一些问题带来一线突破。也许里面说的不尽详情，请你们调查取证。忘记说了：下部队取证的时候，请不要程序来，因为层层派遣的结果，最后还是腐败。最好请首长的亲戚朋友，亲自化妆来部队。或者请您的孙子辈，化名来基层部队当一普通一兵，那么中国部队的所有问题，都在掌中了。总之一句话，从头到尾，从来部队到从部队走人，都是腐败。<br />
<br />
我们总不能扛着腐败，来保卫祖国吧？</div>

							
							
							
							
