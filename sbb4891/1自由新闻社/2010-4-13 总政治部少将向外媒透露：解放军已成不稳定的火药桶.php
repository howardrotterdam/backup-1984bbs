(博讯北京时间2010年4月09日 首发 - 支持此文作者/记者)<br />
&nbsp; &nbsp; 【博讯编者按：博讯曾经报道解放军内买官卖官严重，野战军县团级以上的职务几乎都被高干子弟、军人子弟和关系户子女垄断，平民出身的军人，不管你有多优秀，要想升至野战军的县团级是很难的，而在解放军正师级大校以上军官中，高干和军人子弟几乎占了84%，这个比例里还不包括无法统计的将军的女婿们。这些天，博讯收到了大量来自军队内部的邮件，但由于大多内容无法证实，并未登出。这次收到的来自总政治部少将的来信，由于他以前大校的时候就和博讯有联系，当时他为升官苦恼，最后决定花费借来的80万“搞定”一位老将军的女婿，算是升上了将军行列。他在来信中透露一些新的情况，编辑删除了一些可能暴露他的身份，以及有可能泄露国家机密的内容，刊登如下】<br />
<br />
一位去过河南与陕西接新兵的老乡回来，带给我六瓶茅台，十八条中华香烟，我说，你也搞腐败，是不是看到我升少将了，想巴结我？他说，下去接兵，人家送的，我用不了。我才不想升将军，要是升将军了，还有机会去接新兵？ (博讯 boxun.com)<br />
<br />
我问他，接新兵也能受礼？他神秘地说，首长，亏你还是将军，你知道嘛，现在一个新兵，在陕西的平均价钱是3万元人民币，在河南是2万5千元，女兵的价钱更高，最高的达到18万一个指标。(boxun.com)<br />
<br />
我一下子没有听懂，你这是什么意思？谁给谁钱？只听说国民党当时拉壮丁，难道我们还招不到兵？少校说，你搞反了，是那些想送孩子当兵的人送钱给征兵办，征兵办送礼给我们。他解释说，由于就业率很低，军队的待遇很好，而且当几年兵后如果升上去，过几年转业就有了保障。所以，现在想送孩子当兵的很多，至于一些女兵，很多是送到一些舒服的机关为当官的服务的，机遇更多。<br />
<br />
我问他，要当兵就当兵，用得着送钱？他说，自从征兵的权力被下放到地方后，地方政府的腐败也渗透进征兵工作中，你想当兵，只要不送钱，不管你多健康，绝对过不了关。但只要你送钱，哪怕你是傻子和弱智，也能送进中国人民解放军里。他随即告诉我，我们每年都招收大量残疾、弱智和其他不合条件的新兵入伍，但知道我们要复查，所以地方上收到巨额金钱的家伙也会分一些给我们。我不想当将军，只求你在权限范围内，永远让我负责到各地接收新兵。但他也强调，不能到沿海地区，特别是深圳那些地方，在那里，没有人愿意当兵。<br />
<br />
其实，这些事我也听说过一些，但由自己最信任的老乡口里亲口说出来，而且，他竟然好像一点也不怕我翻脸去举报他，让我觉得很悲哀。想一想，就在上次送钱升官的时候，我也临时向他借过六万元（后来很快就还了）。这一切都让我感觉到，这个国家不像一个国家，这个军队又何来一个军队的样子？<br />
<br />
可是，虽然现在军队几乎都被高干和将军的子弟把持着，而且，又涌进了那么多花钱买来的军人，我要说的是，整个军队，仍然是普通老百姓在卖力，这个国家真要打仗，还得靠我们。不过，我猜中央高层也知道今中国解放军的状况，如果真要打仗，这个政权一夜之间就会土崩瓦解，因为那些高干和将军子弟早就把大笔财产转移到海外，而那些花钱买军服的人，会去为你卖命吗？<br />
<br />
看到吉尔吉斯发生骚乱，军队一夜之间反目，我很有感触，如果中国发生反对派骚乱，军队真会站在当今当权者一边吗？20年前六四屠杀之前，几乎每一个进城的军队的军官都发生了异心，当时邓小平就发现，那些军长师长都不是高干子弟，那可是他在俞正声弟弟叛逃美国后做出的决定（不再信任高高子弟），后来他就在江泽民当政的时候告诫，还是要培养自己的人，至少在军队里要使用自己的孩子更放心。<br />
<br />
但是，即便如此，如果中国现在发生骚乱，那些“将军们”也许会下令屠杀，但他们得记住，军队的灵魂，始终是穷苦的人民和有抱负的人，当他们有钱和子弹的时候，他们不一定会听你的。我们被这个腐败的体制压制得受不了，也受够了。等到有那么一天，只要有人振臂一呼，我们一定不会让他们失望。(boxun.com)<br />
<br />
给博讯写信，是因为我知道总政治部和总参谋部都有专门人员盯住博讯，我希望他们看到我的信，不用追查我是谁，他们只要去检查一下今年从河南和陕西招收上来的军人（包括武警部队）的身体健康，就知道，那里几乎达到三分之一不合格的新兵是如何招收进来的。同时，他们不仿公布一下解放军四总部中正师级以上军官的家庭背景。让民众看看，他们除了把国家的资产收归自己之外，也把枪杆子闹闹抓在手里。</div>

							
							
							
							
