																											<div id="postmessage_34097" class="t_msgfont">四 年 浩 劫 200 万 人 送 命<br />
O果日, 2009-2-18<br />
<br />
整 整 一 代 柬 埔 寨 人 ， 绝 对 忘 不 了 1975 年 4 月 17 日 。 经 过 多 年 战 乱 ， 首 都 金 边 的 居 民 高 兴 地 迎 接 赤 柬 ， 因 为 赤 柬 赶 走 了 透 过 军 事 政 变 夺 权 的 腐 败 政 府 ， 解 放 全 国 。 谁 想 到 这 是 杀 戮 战 场 噩 梦 开 始 ， 赤 柬 所 谓 的 「 社 会 主 义 天 堂 」 ， 其 实 是 极 权 炼 狱 ， 人 民 受 尽 摧 残 虐 杀 ， 国 家 失 去 四 分 之 一 人 口 ， 逃 出 鬼 门 关 的 人 ， 心 灵 创 伤 毕 生 难 愈 。<br />
<br />
助 越 共 抗 美 国 搞 长 期 革 命<br />
<br />
柬 埔 寨 共 产 党 於 1950 年 代 成 立 ， 「 赤 柬 」 名 字 来 自 法 语 Khmer Rouge ， 初 期 是 越 共 分 支 ， 1960 年 代 助 越 共 抗 美 ， 美 军 对 越 柬 边 境 的 轰 炸 ， 激 发 成 千 上 万 柬 埔 寨 人 加 入 赤 柬 。 赤 柬 魔 头 波 尔 布 特 （ Pol Pot ） 亦 乘 时 而 起 。<br />
<br />
波 尔 布 特 生 於 小 康 之 家 ， 小 时 候 连 杀 鸡 都 不 敢 ， 到 1948 年 留 学 法 国 期 间 ， 开 始 对 共 产 思 想 产 生 兴 趣 ， 回 国 後 就 加 入 了 赤 柬 ， 1960 年 成 为 领 袖 。 1970 年 朗 诺 （ Lon Nol ） 发 动 军 事 政 变 ， 推 翻 施 汉 诺 亲 王 （ Norodom Sihanouk ） 统 治 。 赤 柬 与 朗 诺 政 府 为 敌 ， 因 而 得 到 流 亡 中 国 的 施 汉 诺 支 持 ， 势 力 日 增 。<br />
 <br />
朗 诺 政 权 倒 台 ， 赤 柬 攻 入 金 边 ， 民 众 欢 喜 不 了 半 天 ， 事 情 就 急 转 直 下 。 赤 柬 将 国 名 改 为 民 主 柬 埔 寨 ， 宣 布 这 天 开 始 是 「 元 年 」 ， 事 实 是 柬 埔 寨 开 始 血 腥 和 荒 谬 的 四 年 。 波 尔 布 特 曾 访 华 ， 对 於 毛 泽 东 的 文 革 搞 得 「 有 声 有 色 」 ， 决 定 把 毛 那 套 长 期 搞 革 命 搬 到 柬 埔 寨 ， 作 为 改 造 社 会 的 计 划 蓝 本 。<br />
<br />
家 庭 解 体 饥 民 被 迫 吃 人 肉<br />
<br />
赤 柬 的 恐 怖 统 治 第 一 步 ， 是 把 城 市 的 人 全 部 赶 离 家 园 。 四 天 之 内 ， 有 「 东 方 巴 黎 」 之 称 的 金 边 ， 200 万 人 走 得 一 个 不 剩 ， 变 成 鬼 域 。 这 样 是 为 了 打 造 「 社 会 主 义 天 堂 」 ─ ─ 人 人 平 等 ， 没 有 私 产 ， 更 没 阶 级 之 分 。<br />
 <br />
<br />
人 们 失 去 所 有 财 产 ， 要 到 乡 郊 做 苦 工 ， 家 庭 要 解 体 ， 分 为 男 女 住 集 体 宿 舍 ， 夫 妻 相 聚 要 先 取 得 批 准 。 人 人 强 制 劳 动 ， 并 要 一 起 在 公 社 食 堂 进 食 ， 但 其 实 人 人 吃 不 饱 。 每 天 都 有 人 饿 死 ， 最 後 饿 得 要 吃 死 人 的 肉 。 四 年 间 ， 至 少 有 100 万 人 因 饥 饿 丶 过 劳 和 疾 病 而 丧 生 。 为 了 建 立 「 最 纯 粹 的 社 会 主 义 社 会 」 ， 凡 在 前 政 权 服 务 过 ， 或 对 新 政 权 不 满 的 人 ， 甚 至 只 是 说 外 语 ， 都 格 杀 勿 论 ， 普 通 人 也 被 诬 蔑 是 间 谍 遭 屠 杀 。 柬 埔 寨 原 有 700 多 万 人 口 ， 经 过 赤 柬 四 年 统 治 的 浩 劫 ， 暴 跌 了 200 万 ， 大 概 少 了 25% ， 全 国 再 难 找 到 一 个 完 整 的 家 庭 。<br />
