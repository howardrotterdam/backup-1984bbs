													<h2>赤 柬 受 审 　 迟 来 30 年 的 公 义</h2>
						
						
																											<div id="postmessage_34095" class="t_msgfont">O果日, 2009-2-18<br />
<br />
被 告 S-21 监 狱 狱 长 康 克 由 　 <br />
割 喉 摔 婴 儿 　 钻 机 采 脑 进 补 　 酷 刑 惨 无 人 道<br />
<br />
<br />
在 赤 柬 1975 至 1979 年 的 暴 政 统 治 下 ， 柬 埔 寨 变 成 杀 戮 战 场 ， 死 难 者 逾 200 万 ， 写 下 人 类 历 史 最 暴 烈 残 酷 黑 暗 的 一 页 。 赤 柬 暴 政 结 束 30 年 ， 直 至 昨 天 （ 周 二 ） ， 杀 戮 战 场 上 的 暴 行 终 於 开 审 。 在 柬 埔 寨 种 族 灭 绝 法 庭 上 ， 首 个 受 审 的 赤 柬 杀 人 魔 ， 是 当 年 在 首 都 金 边 S-21 监 狱 的 狱 长 康 克 由 （ Kaing Guek Eav ） ， 他 在 恐 怖 监 狱 内 常 说 「 杀 丶 杀 丶 杀 」 ， 用 割 喉 丶 钻 脑 丶 活 摔 婴 儿 等 残 酷 手 段 杀 人 ， 令 15,000 名 被 投 进 入 监 的 人 ， 除 了 少 数 20 多 人 ， 全 部 有 入 无 出 没 活 口 。 柬 埔 寨 人 对 他 受 审 ， 高 呼 是 「 迟 来 的 公 义 」 。<br />
<br />
柬 埔 寨 种 族 灭 绝 法 庭 专 为 审 讯 赤 柬 暴 行 而 设 。 现 年 66 岁 的 康 克 由 乘 坐 装 甲 车 前 往 法 庭 ， 之 後 一 动 不 动 地 坐&nbsp;&nbsp;庭 内 的 防 弹 玻 璃 内 ， 被 控 侵 犯 人 权 丶 战 争 罪 行 丶 虐 等 和 谋 杀 等 罪 名 。 昨 天 只 是 处 理 法 律 程 序 ， 下 月 底 开 始 传 召 证 人 作 供 ， 料 9 月 有 裁 决 。 改 信 基 督&nbsp;&nbsp;的 康 克 由 ， 早 前 承 认 在 S-21 监 狱 犯 下 很 多 罪 行 ， 但 声 称 只 是 奉 命 行 事 ， 去 年 更 流 泪 哀 求 受 害 人 家 属 宽 恕 ： 「 我 知 道 你 们 永 远 不 会 原 谅 我 ， 但 请 给 我 一 线 希 望 。 」 他 的 律 师 说 ， 他 还 希 望 得 到 柬 埔 寨 人 的 原 谅 。<br />
<br />
S-21 监 狱 变 万 人 冢 博 物 馆<br />
<br />
求 原 谅 ， 谈 何 容 易 。 柬 埔 寨 人 对 康 克 由 狠 之 入 骨 ， 等 他 受 审 等 了 30 年 。 法 庭 外 一 早 挤 了 1,000 人 ， 要 排 队 旁 听 ， 有 人 从 240 公 里 外 前 来 ， 要 亲 眼 看 见 他 没 好 下 场 ， 要 质 问 他 为 何 那 麽 残 忍 。 有 受 害 人 亲 属 高 呼 要 公 义 伸 张 ， 18 名 亲 属 在 赤 柬 统 治 期 间 被 杀 的 森 尤 索 说 ： 「 赤 柬 政 权 残 酷 不 仁 ， 屠 杀 自 己 人 民 。 我 从 没 想 过 会 平 反 ， 但 这 天 终 於 来 临 。 」 63 岁 的 万 纳 ， 当 年 被 投 入 S-21 监 狱 ， 因 要 替 波 尔 布 特 画 肖 像 ， 才 避 过 一 死 。 他 说 ： 「 不 止 是 我 ， 所 有 柬 埔 寨 人 都 等 了 30 年 。 」<br />
 <br />
<br />
康 克 由 拥 有 中 国 人 血 统 ， 绰 号 「 杜 赫 」 （ Duch ） ， 年 轻 是 一 位 深 受 学 生 爱 戴 的 小 学&nbsp;&nbsp;师 ， 後 来 追 随 波 尔 布 特 投 身 共 产 革 命 ， 赤 柬 统 治 柬 埔 寨 期 间 ， 是 S-21 监 狱 ， 专 门 以 残 暴 手 段 拷 问 政 治 犯 。 S-21 酷 刑 招 式 惨 无 人 道 ， 包 括 割 喉 丶 钻 脑 丶 活 摔 婴 儿 等 ， 传 闻 为 了 给 领 导 人 进 补 ， 狱 中 还 特 制 钻 脑 机 ， 然 後 将 异 见 人 士 绑 在 椅 子 上 ， 用 快 速 旋 转 的 钻 头 ， 从 受 害 者 的 後 脑 钻 入 ， 活 生 生 取 出 脑 袋 。 康 克 由 经 常 亲 自 参 与 拷 问 ， 会 发 施 号 令 集 体 处 决 ： 「 杀 光 他 们 。 」 一 次 审 问 29 名 囚 犯 时 ， 他 竟 说 ： 「 我 只 盘 问 四 人 ， 其 他 给 我 杀 光 ！ 」<br />
<br />
杀 丶 杀 丶 杀 ， 是 赤 柬 政 权 的 血 腥 暴 行 ， 暴 政 不 能 长 久 ， 终 於 1979 年 倒 台 。 赤 柬 倒 台 後 ， 康 克 由 改 名 换 姓 ， 直 至 1999 年 被 捕 。 而 S-21 监 狱 也 改 建 成 Tuol Sleng 屠 杀 博 物 馆 ，&nbsp;&nbsp;面 一 排 排 万 人 冢 骷 髅 ， 见 证 了 这 段 20 世 纪 最 血 腥 的 屠 杀 和 康 克 由 的 深 重 罪 孽 。<br />
<br />
除 了 康 克 由 外 ， 另 外 四 名 前 赤 柬 领 袖 也 被 羁 押 ， 今 年 稍 後 受 审 ， 届 时 康 克 会 转 作 证 人 ， 指 控 他 们 的 罪 行 。 他 们 包 括 前 赤 柬 第 二 号 人 物 农 谢 丶 三 号 人 物 英 萨 利 和 他 曾 任 社 会 部 长 的 妻 子 英 蒂 利 丶 前 国 会 主 席 乔 森 潘 ， 四 人 若 被 裁 定 罪 成 ， 最 高 刑 罚 是 终 身 监 禁 。<br />
