													<h2>外交部：红色高棉是柬内政，吃饱饭没有事的外国人不要折腾</h2>
						
						
																											<div id="postmessage_34153" class="t_msgfont">新华网2月17日报道 2009年2月17日，外交部发言人姜瑜举行例行记者会，主要就美国国务卿希拉里访华、中美关系、朝鲜半岛局势等答记者问。<br />
<br />
姜瑜：女士们、先生们，下午好。今天我没有要主动发布的消息，现在我愿意回答各位的提问。<br />
<br />
问：美中经济安全评估委员会目前正在开会，讨论国际金融危机的起因和中国在应对危机过程的作用。你对此有何看法？<br />
<br />
答：中国领导人和中国政府相关部门已在多个场合阐述了中国政府应对国际金融危机的态度。我们认为，国际社会应共克时艰，加强合作，携手共同应对这场全球性金融危机。中国政府采取了一系列有力措施，包括实施积极的财政政策和适度宽松的货币政策，保增长，扩内需，深化改革，扩大对外开放。我们希望通过保持自身经济平稳较快发展，为促进世界经济增长和维护国际金融市场稳定做出自己的贡献。同时我们也本着负责任的态度，积极参与应对金融危机的国际合作。<br />
<br />
追问：你刚才的回答是介绍中国的应对措施，能否说明中国如何看待金融危机的起因？<br />
<br />
国际社会对国际金融危机产生的原因及应对措施都有共识。我们希望各国加强宏观经济政策的沟通与协调，推动国际社会更多地关注发展问题，建立公正、公平、包容、有序的国际金融体系，增加发展中国家的发言权和代表性。中方愿与国际社会一道推动伦敦金融峰会取得更多积极成果。<br />
<br />
问：有两个关于越南的问题。第一，今天是中越战争开始30周年，中国计划如何纪念这场战争？第二，目前有一位梵蒂冈官员正在越南访问，打算同越南正式建立外交关系。有人说，越南只有在中国同意的情况下才会同梵蒂冈建交。你有何评论？<br />
<br />
答：多年以前，中越两国领导人已就“结束过去，开辟未来”达成共识。目前中越关系发展良好，各领域交流与合作日益密切。我们将坚定地奉行“与邻为善，以邻为伴”的周边外交方针，也愿与越方共同努力，不断发展中越睦邻友好合作。<br />
<br />
去年5月中越双方建立了全面战略伙伴关系，双边关系进入新的发展阶段。两国领导人保持频繁接触，经贸合作日益扩大，两国陆地边界勘界立碑工作如期完成，各领域交往与合作正在向新的深度和广度扩展。中越两国发展平稳、友好的关系符合两国和两国人民的根本利益，也有利于地区和世界的和平、稳定与发展。我们对中越关系的发展前景充满信心。<br />
<br />
关于第二个问题，中方从不干涉别国内政，我们不会做你刚才提到的那种事。关于中梵关系，我们的立场是一贯的、明确的。<br />
<br />
问：美国国务卿希拉里开始了她的亚洲之行。有美国媒体报道，这次希拉里访问的主要目的是中国。你怎样评价希拉里的中国之行？能否介绍她访华行程的具体安排？会签署哪些协议？<br />
<br />
答：应杨洁篪外长邀请，希拉里国务卿将于20至22日访华。双方将就两国关系以及共同关心的国际和地区问题深入交换意见。我们会按惯例提前将有关活动安排告知去现场采访的记者，也会及时对外发布有关活动的消息。<br />
<br />
我们注意到希拉里国务卿近日就美国的亚太政策发表演讲，强调美方将与亚洲国家“同舟共济”，开创双方合作的新时代。我们欢迎美国为亚太地区的和平、稳定与发展多发挥建设性的作用。<br />
<br />
我们也赞赏希拉里国务卿关于美方愿与中方发展积极的合作关系的表态。中方认为，一个良好的中美关系符合两国和两国人民的利益，也有利于亚太乃至世界的和平与发展。我们愿与美方共同努力，加强对话、增进互信、扩大合作，推动中美关系在新的时期沿着健康稳定的轨道向前发展。<br />
<br />
问：希拉里访华期间将有哪些经贸方面的活动？她是否将会见王歧山副总理？<br />
<br />
答：我现在还没有拿到希拉里国务卿访华的最终日程，有关活动我们会及时对外发布消息，你也可以申请去现场采访。<br />
<br />
问：希拉里国务卿访华期间是否会与中方讨论孟买恐怖袭击事件和美印核合作？<br />
<br />
答：中美双方会就共同关心的广泛议题交换意见，包括国际和地区热点问题，我现在无法预测双方会谈及哪些问题。<br />
<br />
关于印巴关系，我们欢迎一切有助于缓和印巴紧张关系的举措，希望印巴双方通过对话协商妥善处理有关问题。我们注意到近期双方就有关问题进行了合作，双方都强调要坚决打击恐怖主义。作为印巴两国共同的朋友，我们真诚希望印巴双方从南亚和平稳定的大局出发，共同致力于维护地区稳定，推进南亚和平进程。<br />
<br />
关于美印核合作，我们的立场是一贯的，没有变化。<br />
<br />
问：希拉里国务卿访华期间，中美会谈的具体议题是什么？是否谈及人权、贸易、人民币汇率等问题？第二，关于日本国内问题，日本财务大臣今天表示，因工作不力，他不久将会辞职，你对此有何评论？<br />
<br />
答：关于希拉里国务卿访华的议题，中美双方会就广泛和共同感兴趣的议题深入交换意见。关于人权问题，中美两国历史文化、社会制度和发展阶段都存在差异，中美在人权问题上存在分歧也属正常。自1990年以来，中美双方已经举行了多次人权对话，对增进双方了解、缩小分歧、扩大共识，共同提高，起到了积极作用。我们愿意在平等、相互尊重和互不干涉内政的基础上，继续与美方开展人权对话，并希望对话能够取得积极成果。关于你提到的贸易、汇率等其他议题，如果双方都有兴趣的话，可能会谈到。关于中美经贸关系等问题，建议你向中方主管部门了解有关立场。<br />
<br />
第二个问题，就像你说的，这是日本的国内问题，我们不对属于其他国家内政问题进行评论。<br />
<br />
<br />
<br />
<br />
问：我有两个问题。第一，有报道说，朝鲜计划试射远程导弹，中方对此有何回应？第二，中方是否支持柬埔寨政府以“种族灭绝罪”审判前红色高棉领导人？有人权组织指责红色高棉政权是在中国的支持下犯下战争罪，你对此有何评论？<br />
<br />
答：关于第一个问题，我们希望有关各方都能够认识到，维护朝鲜半岛的和平稳定，符合有关各方的共同利益。我们希望有关各方通过对话协商解决彼此的关切，改善关系，增进互信，共同致力于维护朝鲜半岛的和平与稳定。<br />
<br />
关于第二个问题，审判前民柬领导人是柬埔寨的内政，我们希望国际社会能够尊重柬埔寨政府和人民的选择。<br />
<br />
问：我的两个问题是关于朝鲜的。你是否认为朝鲜试射导弹违背了和平对话解决半岛问题的原则？昨天是金正日的生日，中方是否向其表示了祝贺？<br />
<br />
答：关于第一个问题，我已经清楚地阐述了中方的立场。我们希望有关各方能够共同努力，维护朝鲜半岛的和平与稳定，这符合有关各方的共同利益，也是国际社会的共同愿望。<br />
<br />
关于第二个问题，金正日总书记昨天生日，我们也祝他生日快乐。今年是中朝建交60周年，也是“中朝友好年”，中朝双方会以此为契机，通过举办一系列的交流活动，促进双方的友好合作关系。我们希望与朝方一道，推动两国关系不断取得新的进展。<br />
<br />
问：本周末，巴基斯坦总统扎尔达里将赴武汉、上海等地访问，请问巴总统此行是否会与中方讨论巴对华债务问题，中方是否会考虑免除巴对华债务？<br />
<br />
答：扎尔达里总统将于本月20至23日对湖北和上海进行访问，这是他去年10月对中国进行国事访问之后的第二次访问。这次访问的目的主要是考察中国的水利、农业和金融领域的发展情况，以推动中巴双方在上述领域的合作。当前中巴保持着良好的合作关系，两国的高层交往密切，经贸、农业、交通、能源等领域的合作进展顺利，去年，中巴双边贸易额达到了69.8亿美元。双方在国际和地区事务中进行了密切的协调与配合。中巴之间的合作不仅符合双方的利益，也有利于地区的和平、稳定与发展。我们高度重视扎尔达里总统此次访华，相信此访一定能够推动中巴全天候友谊和全方位合作深入发展。<br />
<br />
中方一直在力所能及的范围内为巴基斯坦的经济、社会发展提供帮助。关于你的具体问题，我目前没有可以向你提供的信息。我们希望巴基斯坦保持稳定，实现经济发展和社会和睦。<br />
<br />
问：关于审判红色高棉领导人的问题，刚才还提到人权组织指责中国对红色高棉领导人的支持。你对此有何回应？<br />
<br />
答：中国一贯是在和平共处五项原则基础上发展同周边国家的友好合作关系，这种关系是正常的。<br />
<br />
长期以来，中国在和平共处五项原则的基础上同包括民主柬埔寨在内的柬历届合法政府保持了正常友好的关系。众所周知，当时的民主柬埔寨政府在联合国拥有合法席位，同70多个国家建立了广泛的外交关系。<br />
<br />
我们希望国际社会充分尊重柬政府和人民的选择。<br />
<br />
问：我有两个问题。首先，中国外交部部长助理吴红波将要访问捷克和斯洛伐克，能否介绍有关情况。他此访是否会讨论中欧峰会的问题？第二个问题，津巴布韦总统穆加贝已经或将要在香港置业。中国政府对此是否表示关切？支持还是反对其购买房产？<br />
<br />
答：关于你的第一个问题，外交部部长助理吴红波于15日开始赴捷克、罗马利亚、匈牙利、斯洛伐克、阿尔巴尼亚和希腊进行双边政治磋商。我们希望通过此访落实中国与上述各国外交部的磋商机制，深化双方外交部之间的合作，推动中国与上述各国的友好合作关系深入发展。<br />
<br />
关于你的第二个问题，我不了解你提的这个情况，我也不对猜测性的报道进行评论。<br />
<br />
问：中国中联部部长王家瑞将访问阿根廷、哥斯达黎加等几个拉美国家。中国国家副主席习近平也正在拉美一些国家访问。你能否介绍一下此次访问的目的、议题和会见安排等有关情况？访问期间是否将签署经贸协议？<br />
<br />
答：我没有得到你提的这个访问的消息，建议你向有关部门去询问。中国国家副主席和副总理目前正在拉美访问，关于他们的活动我们都及时发布了消息。<br />
<br />
中国和拉美各国同属发展中国家，我们面临着共同的发展任务。在和平与发展问题上，中国和拉美国家有着广泛的共同利益。我们致力于同拉美国家建立平等互利和共同发展的全面合作伙伴关系。<br />
<br />
感谢各位出席，再见！ (本文来源：新华网 )</div>

							
							
							
							
