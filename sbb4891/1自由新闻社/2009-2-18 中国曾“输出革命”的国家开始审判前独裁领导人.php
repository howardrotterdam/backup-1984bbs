　　&nbsp;&nbsp;<br />
那年八月，大寨遭受了罕见的风灾和雹灾的袭击。英雄的大寨人以惊人的毅力，科学的态度和辛勤的劳动，战胜了灾害，夺得了丰收。波尔布特等在陈永贵、王谦、郭凤莲等陪同下，登上虎头山，观看大寨的丰收景色。在虎头山下一块块平展展的梯田里，社员们挥汗收割，紧张播种；打谷场上脱粒机轰鸣，金黄色的谷粒在高空飞扬。柬埔寨客人看着大寨人用勤劳的双手描绘出这欣欣向荣的山村景色，都十分高兴。他们兴致勃勃地参观昔日的乱石沟、今日的米粮仓狼窝掌，以及新建成的蓄水池，硕果累累的果木林，肥猪满圈的饲养场。柬埔寨客人们一边参观，一边高兴地向群众招手，并且不时地停下来同大家一合影留念，欢声笑语洋溢田间。&nbsp;&nbsp;<br />
