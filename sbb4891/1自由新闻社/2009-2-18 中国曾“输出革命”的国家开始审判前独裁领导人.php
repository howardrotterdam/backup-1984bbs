赤 柬 大 屠 杀 的 暴 行 ， 令 人 齿 冷 ； 但 赤 柬 的 幕 後 支 持 者 ─ ─ 中 共 政 权 一 直 没 就 支 持 暴 政 道 歉 ， 至 昨 天 （ 周 二 ） 仍 自 辩 那 是 跟 柬 埔 寨 外 交 关 系 正 常 化 的 一 部 份 。<br />
