																											<div id="postmessage_33891" class="t_msgfont">柬埔寨特别法庭开始审判前红色高棉领导人<br />
<br />
联合国支持下的柬埔寨法院特别法庭（Extraordinary Chambers in the Courts of Cambodia , ECCC）当地时间星期二在金边（Phnom Penh）开始正式审讯红色高棉（Khmer Rouge）政权时期的重要人物康克由（Kaing Guek Eav），为该政权３０年前所犯下罪行追讨正义与问责的努力由此向前迈出了重要一步。请听联合国电台记者程浩的报道：<br />
<br />
联合国与柬埔寨于２００３年６月签署设立柬埔寨法院特别法庭的协议，对被指在上个世纪七十年代后期在柬埔寨犯下严重罪行的前红色高棉领导人进行审判。２００６年，特别法庭正式成立，但对相关人员的审讯工作由于柬方的不配合以及资金不足等原因而拖延至今。<br />
<br />
作为红色高棉统治时期掌管首都金边监狱的负责人，康克由当年还负责一个被称为S２１的政治集中营，对１万６千多名被怀疑是红色高棉政权&quot;敌人&quot;的人员进行严刑折磨和拷问，其中绝大多数人最后被处死。秘书长发言人蒙塔斯当天援引联合国红色高棉审判援助团（UNAKRT）的声明指出，康克由是红色高棉政权倒台３０年来第一个受到审判的高层领导人。<br />
<br />
蒙塔斯：&quot;（英语）康克由此次面临的指控包括危害人类罪等严重违反１９４９年《日内瓦公约》的罪行，此外还依据柬埔寨刑法被指控犯下谋杀、虐待等罪行。特别法庭表示，柬埔寨人民为这一天已经等待了３０年，要为所有遭受苦难的人寻求公道。当年柬埔寨三分之一的人口在红色高棉统治期间死亡。&quot;<br />
<br />
在１９７５年４月至１９７９年１月的三年零八个月时间里，柬埔寨&quot;红色高棉&quot;在柬执政，其领导人实施所谓&quot;从肉体上消灭资产阶级&quot;的极端政策，并进行党内政治大清洗，造成数以万计柬埔寨人因劳役、迫害、饥荒而非正常死亡，成为２０世纪中最血腥暴力的人为灾难之一。<br />
<br />
