													<h2>北川中学的豆腐渣教学楼是这样做成的</h2>
						
						
																											<div id="postmessage_91311" class="t_msgfont"><img src="http://i40.tinypic.com/34dn21y.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
图则证川校豆腐渣<br />
钢筋幼石屎劣 专家揭六大问题<br />
<br />
3名北川中学遇难学生的家长，偷偷进入埋葬了他们孩子的废墟，前方2003年落成的教学楼完全粉碎性倒塌，旁边1999年启用的校舍（灰色身）则只有天花脱落，远方黄色身红顶的校舍则几乎完好无缺。（明报记者摄）<br />
<br />
<br />
　【明报专讯】今天是四川大地震一周年，四川当局过去一直否认有人死于「豆腐渣」工程，但本报取得活埋逾千师生的北川中学的施工图则，发现教学楼的建筑质量最少出现6个问题，包括图则不专业、部分钢筋太幼，以及钢筋交接的驳位太短等，全都削弱了大楼的抗震能力。3名工程师分析后，均直言学校建造质素差，更有工程师指大楼只可抵受3至4级地震。<br />
<br />
　四川大地震一周年，四川多个地区有纪念活动。据了解，国家主席胡锦涛和国务院副总理李克强会到震央映秀镇出席纪念活动。而今午2时28分的一年前大地震发生时刻，全市会鸣笛3分钟向死难者致哀。<br />
<br />
　料只可御三四级地震<br />
<br />
　川震伤亡惨重，「豆腐渣」工程被视为「元凶」之一。两名曾多次到灾区察看的资深工程师，将北川中学施工图则与现场废墟比较，直言该图设计不良，施工现场亦存在钢筋过幼、间距过宽、梁柱接驳散等严重问题，其中一人指出s「某些学校钢筋都没有，1级地震都会倒下，北川中学仍有钢筋，但勉强只可抵受3至4级地震，与当地7级防震标准相差太远。」<br />
<br />
　这名不愿透露姓名的工程师呼吁s「中央必须以理性来正视普遍存在的豆腐渣问题……核心意义是展望未来，防止中国再发生同类事件。特别是上海等接近地震带的大城市，有必要作建筑质量普查，并修订条例，严格检测施工质量。」<br />
<br />
　北川中学原有4幢大楼，其中于2003年落成的北川中学教学楼，在5．12地震中粉碎性倒塌，活埋1300人。本报记者5月初于现场所见，教学楼外，另外3幢大楼损&#63108;并不严重，如旁边一幢1999年完工的旧校舍只有天花脱落，另一边于70年代落成的老式建筑更完好无缺。<br />
<br />
　家长得图 公安恐吓泄机密<br />
<br />
　学生家长母勇贤早前冒险取得教学楼施工图，比对现场瓦砾展开调查，发现工程严重偷工减料。维权人士王笑冬忆述s「母勇贤将施工图}印本交到四川省公安厅，第二天绵阳市公安局来了几个警察，把他带到宾馆以『泄露国家机密』罪名威胁他。」<br />
<br />
　至于王笑冬自己则千方百计，欲将图则交给中纪委，但其间不断被监控及收到死亡恐吓，3月起被迫流亡。内地媒体有关图则的报道则被彻底封杀。<br />
<br />
　本报取得此施工图，印有「北川建筑勘察设计所」字样、签名等资料。今年元旦曾到过北川中学的港大建筑系助理教授朱涛，看过施工图后，认为图则是真的。<br />
<br />
　他对照过施工图和家长调查时于废墟现场拍得的照片，认为家长的调查，反映中学实际施工情?严重不符合施工图则，如当地规定组成混凝土的「石头子」的直径不能大过2厘米，「但从现场的照片所见，竟有10厘米的石头，是很严重的错误！」他解释，石粒过大，土内空间便扩大，钢筋等建材无法抓紧，强度下降易老化，结构便不安全。<br />
<br />
　他再指出建筑物的多个问题，如用于梁柱的钢筋数量减少、位置不对，部分钢筋亦太幼，这些均严重影响该校结构，「若该调查结果是真的……便可确定是豆腐渣工程」。但他指家长的调查只反映学校的局部情?，且多次馀震后，塌下的物料亦需逐一仔细查看，「要政府详细调查才可下结论」。<br />
<br />
　朱涛补充说，90年代的建筑物皆需接受质量检查和提交检查报告，「若详细调查确定与施工图严重不符合的范围，那个施工队、现场监工的人为什麽看不到问题？质量检查的人为何发现不到问题？施工工人是否有倒卖材料？这些人都在，可逐一查出」。<br />
<br />
　朱涛表示，完全根据施工图施工，北川中学应可抵受去年的地震，「只会出现裂痕，但不会整幢塌下来」。<br />
<br />
　对于官方对北川中学变成「废墟」的说法，是认为教学楼的选址问题，朱涛指可透过挖开土壤探察了解这说法是否属实。<br />
<br />
　无国界工程师召集人黎广德认为，当局应设立类似香港的死因聆讯，公开资料及分析建筑倒塌的原因，从中汲取教训，防悲剧重演。<br />
<br />
　明报记者</div>

							
							
							
							
