文章提交者：qiandesen 加帖在 猫眼看人 【凯迪网络】 <a href="http://www.kdnet.net" target="_blank">http://www.kdnet.net</a><br />
<br />
本来不想在这里写点什么，但是看到这里有些人的观点太过偏激，我还是说几句吧。<br />
一、维持朝鲜半岛现状对中国最有利，就像维持中国海峡两岸现状对美国最有利一样。<br />
二、所谓“有利”，是指“以朝压韩”和“以韩压朝”的主动权掌握在我们手中，两方谁也不敢太嚣张，谁嚣张，就利用另一方打他一下屁股，我处于绝对超然的位置。<br />
三、如果朝鲜半岛统一，则必然是一个亲美的政权，美国的军队就真正在鸭绿江边虎视眈眈了。目前，我国周边的政权，尚没有一个真正亲美的政权，因此绝对不会让朝鲜半岛成为例外。<br />
四、朝鲜从朝鲜战争结束后，一直处于对抗美国的前沿，如果没有朝鲜在前面顶着，我国在东北亚地区的军事布防和军事策略要极大的修正，这一点中朝两国都十分清楚，从这方面来看，中国是欠朝鲜一些人情的。<br />
五、朝鲜对于中国的意义不是经济方面的，而是政治和军事方面的，更是战略方面的。<br />
六、我再说明白点，如果没有我国在后方撑着，美国早就收拾朝鲜了。美国二战后，收拾了那么多国家，为什么不敢收拾朝鲜？呵呵，还不是看我们的面子。<br />
七、结论。<br />
1.东北亚地区不会有战争，朝鲜叫嚣一阵就会趋于平静，<br />
2.我国绝对不会和美军韩军联合起来对付朝鲜，<br />
3.一旦美韩要对朝鲜动手，我国必然坚决反对，甚至不惜以武力来保卫朝鲜（原因见上述三），<br />
4.我国政府的声明，确实对朝鲜不满，但也主要是给国际社会看看样子，与朝鲜表面上划清界限。实际上，暗地里与朝鲜还是铁哥们。<br />
5.维持朝鲜半岛现状，说白了，就是坚决不能让朝鲜半岛统一。呵呵。</div>

							
							
							
							
