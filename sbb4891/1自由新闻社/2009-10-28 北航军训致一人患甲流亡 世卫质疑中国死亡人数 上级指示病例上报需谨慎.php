---------------------------分割线------------------------------------------------<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;（以下内容节选自一篇名为《不得不说，北航你TMD不地道！！！》的日志）<br />
<br />
以上是虚假消息，只有28人确诊和1人死亡是真的，以下是真实消息（我在连部做宣传工作，所以不需要训练，对情况了解的还是比较多的）<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;北航为了给挑战杯腾宿舍，把大一的撵走去军训，导致大面积流感爆发时，人员不能及时撤离到更好的条件下进行修养隔离。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 军训住宿条件女生我不清楚，男生竟然有40人一间的大宿舍!连北京的工地上都禁止使用这种住房了！！睡在门边的人基本达到被隔离率100%。食堂的饭就不说什么了，正常时可以保证生存，但生病时完全没任何营养可言，并且卫生极差，25，26两天，病号餐具和我们的是串着用的，并且不干净。<br />
<br />
22号下午拉到训练基地，23号上午开营，第一天还算顺利<br />
<br />
24号时，开始有少数人员发烧，大家开始以为是为了逃军训，此时大家还都很乐观，学校并未采取任何隔离措施。<br />
<br />
25号开始，北京降温，仅25号一天，学校旁简陋的卫生室开出假条近200张，其中约150张是流感伴随发热症状，此时高烧不退者开始转到团部大楼3楼进行隔离，但是吃饭还是和普通人一起吃饭。（此时团部2楼是女生宿舍！和病号距离极小）。每天吃饭前在团部大楼前集体集合，组织唱歌，大量吸入有毒气体。（此时大家情绪还不错，病号还探出脑袋冲下边望）<br />
<br />
26号，感冒发烧人员接近500人，学校有所重视，但被隔离人员依旧能和我们一起吃饭，此时情况最严重的7连某班已达到1/3被隔离。<br />
<br />
27号晚，有消息说已经有1000+人感冒并伴随发热症状，但我估计实情没那么多，也就700吧，据说发烧温度低于38度的都进不去隔离房。。。不过北航的领导已经来到训练基地商量对策，从本天起各寝室开始少人，我们寝室最终少了4个，这算很少很少的了。。。。本日没有安排任何训练，大家集体练习歌曲，没有连队隔离措施。团部在下午依旧没拉起警戒线，连队外侧开始安装铁门，用意不说大家也知道了。本日起开始服用”达非“副作用太大，吃完只后体温会小下降。。。头晕晕的。这药希望在营区的同志们别吃了。。。<br />
<br />
28号上午，空军得到一人死亡的消息，于是隶属空军某部队的教官被三辆大巴全部拉走（意思是教官顶不住了）中午比学校阶级更高的领导开始到达，新闻记者到达，此时起，北航校管理部门已丧失了对事态的决策权。（本天上午政策是，发烧的可以被接走，没发烧的不能走，下午则是，发烧的不能走，前几天一直不发烧的可以走）本天起感冒人数应该接近1000了（由于消息封锁，团部拉起了警戒线，我们什么都不知道）晚上，北京新闻拨出，消息不胫而走。女生首先发难顶不住了，男生于约1小时后开始有内部消息。经领导批准，我等迅速逃离了疫区。。。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 还在营地的同志们要挺住啊！！！！！！！！！！！！！<br />
