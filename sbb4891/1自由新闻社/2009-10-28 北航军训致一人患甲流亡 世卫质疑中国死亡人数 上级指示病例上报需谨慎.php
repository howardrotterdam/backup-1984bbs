<br />
　　接到报告后，北京市卫生、教育部门立即督促并协调北京航空航天大学进一步采取隔离、防控措施。其中，体温超过38℃的71名学生已有28名咽拭子检测甲型H1N1流感病毒核酸阳性。目前，患者病情基本稳定，生命体征平稳，无危重病人。<br />
