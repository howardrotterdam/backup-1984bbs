																											<div id="postmessage_305985" class="t_msgfont">北航这一日，积怨爆发……科技放飞生命 挑战成就悲剧『又更新了』<br />
2009-10-29 00:21 | (分类:默认分类)<br />
还记得，曾经我爸告诉我有北航这么一所大学的时候，他介绍时开完笑地说，就是几年前就算了有录取通知书也得交十万才能进，你去的话帮我省十万……<br />
<br />
当他们只给危楼打补丁的时候，我没有说话，因为我不是住那。当他们让伙食又贵又难吃的时候，我没有说话，因为我吃清真。当他们赶走大一以迎挑战杯的时候，我没有说话，因为我不是大一的。当他们毁掉你人生的时候，没有人说话，因为已经没有人了。<br />
<br />
某文：<br />
<br />
---------------------------前言---------------------------<br />
<br />
估计大家心里都有数，就是因为挑战杯大一的孩子才在这个时候去军训的，我在这里留一个问题，北航筹备那么久，想让大一的孩子去沙河，为什么要突然改变主意？<br />
<br />
临时改变决策，进而又大费周章让新生把没住热乎的宿舍全部腾出来，让给外校代表团，然后在深秋让孩子们去高强度训练，我实在不明白校团委如此决策到底是在考虑什么???<br />
<br />
去沙河虽然说分校区上课不便，可是，毕竟还是有很多学校有先例，总归是有方法可循的吧，一定要独树一帜，深秋去军训，才能体现校团委的英明吗？？？<br />
<br />
-------------------------正言-----------------------<br />
<br />
说了正么多，说说挑战杯吧。<br />
<br />
老实说，之前是充满了对挑战杯的期待的，毕竟是工科生，没有实力参赛，还可以做做志愿者，发挥一下余热――这是最开始的天真想法。<br />
<br />
展览展示业务口，共200人左右吧，只集中培训过2次，非常泛泛，泛泛到志愿者只是知道了挑战杯是什么。<br />
<br />
我负责展区讲解员部分的工作，一个比较特殊的部门，同时还要管理“总咨询台”，26个小女孩，2个小男生，只培训过一次，依旧只是一个对讲解的泛泛的了解，向“总咨询台”这种要求很强的业务，没有时间培训过！据我所知，有些业务口一次都没有培训过，关于礼仪、验证、引导等等方式几乎全不熟悉！<br />
<br />
 <br />
<br />
26日，挑战杯开始布展，关于是否出借工具，还没有定论。<br />
<br />
每个小时都可以接到作者的投诉，展位和他们的要求有很严重出入。<br />
<br />
请注意：校团委在很久以前就已经给每位作者发了表格，让他们注明对展位的特殊要求。然后就没有下文了！然后依然按照自己的设想做了一套展位。<br />
<br />
我说一件具体的事，有一件能源化工类作品中含有微生物，微生物在恒温箱中存活，需要2000W电保证其工作，一旦不能满足，微生物就会死去，作品就没有意义了……那个作者一个下午前前后后跑了总咨询台10次，我们除了告诉他“我们也没有办法”之外，只能羞愧的面对这位心急如焚的作者……<br />
<br />
事情的发展是：经理重新统计各个区域需要大功率电的展位，然后，让物业的电工通宵，把大功率线赶了出来……<br />
<br />
作者的评价是：你们赛前完全有充足的时间考虑和准备这种事情，2000W也不算过分，这么多工科项目，很多作品都可能需要的，而且，每个作者都事先提交过“展位需求”，统计的事情应该很久以前就妥当了，为什么要等到比赛了又重新统计通宵赶工？？？<br />
<br />
我的感觉是，完全同意！我身为一个官方志愿者，感到很羞愧，我觉得对不起这些千里迢迢赶来参赛的作者们！对不起！己所不欲，勿施于人。北航就没有一点“得道者多助，失道者寡助。”的概念吗？？？把自己的展位安排在最好的位置也就罢了，自己招摇的摆上三、四十寸的显示屏招摇也就罢了，就不能稍微考虑考虑别的参赛选手吗？？？人家也一样是科技精英啊！！！<br />
<br />
 <br />
<br />
再说说我们所谓的“总咨询台”：<br />
<br />
请问你们能借一下插线板吗？对不起我们不提供。<br />
<br />
请问你们帮忙寄存东西吗？对不起我们不保全。<br />
<br />
请问你们能借一下小推车运设备吗？对不起我们没有。<br />
<br />
请问你们知道XX票在哪领吗？对不起我们不知道。<br />
<br />
请问你们知道今天的行程安排吗？对不起我们不知道。<br />
<br />
请问你们知道评审的流程吗？对不起我们不清楚。<br />
<br />
请问你们有会务手册吗？对不起我们没有……<br />
<br />
对了，我们的官方展位查询手册上少印了一页，有20个学校几十件作品无法查询……<br />
<br />
你觉得这是咨询台吗？？？<br />
<br />
呵呵，我现在都一点不怵那些投诉的人，我觉得他们说的特对，然后我记下来，等着领导跟我说一句“那个，咱们不管。”然后就只好继续接受相同相似的重复投诉……<br />
<br />
我们和后勤、接待、票务、赛事、评审……所有部门都没有沟通，我们什么也不知道，什么信息也提供不了，每每最后就说一句，您问一下组委会吧，然后作者愤愤的说：你们不就是组委会吗？！！<br />
<br />
 <br />
<br />
嗯，我终于知道中国社会踢皮球一样的问题解决方式是怎么出来的了……我的咨询台是一个皮球中转站，有人踢过来，我们又踢走……<br />
<br />
-----------------------------------------------------------------------------<br />
<br />
今天是评审日，昨天就有作者问我们评审流程，我们仍旧一问三不知……<br />
<br />
然后说今天的评委，评委的评审顺序和展位顺序完全不同！！！六、七十岁的老教授老专家满场馆的乱走找不到自己要评的项目……展位查询手册上的字起码是小六号，辛苦各位评委老师了！……<br />
<br />
-------------------------------------------------------------------------------<br />
<br />
明天是原定计划的团体参观日，因为那个著名的新闻，活动取消了……我业务口25个讲解员成了无业游民……<br />
<br />
好吧，就先到这吧……<br />
<br />
还是那句话，我心疼小志愿者们，可怜挨冻的大一孩子们，完全理解任何作者的任何抱怨……<br />
<br />
最后，鄙视校团委，<br />
<br />
鄙视的原因：你做了这么的牺牲，哪怕是把挑战杯组织好也成啊！一定要这么凑合吗？？？<br />
<br />
某文：<br />
<br />
北航你已经被评为退步最快的10所大学了，你要是再不重视本科生你就完了<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;出事了，出事都是偶然的，偶然事件就有一个概率问题，所以北航出事了，或者说是必然的。因为在北航心目中研究生只是他们赚钱的工具，廉价的劳动力，而本科生他们连看都不看一眼。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;记得我们军训那年，一个同学背部被生满铁锈的角铁划出了很长很深的口子，我们伟大的校医院居然没有带破伤风去军训基地，当我陪同学去看病的时候，医务室只是拿酒精擦了擦就了事，我问他们：“难道不打破伤风吗？”回答是没有，然后也没有说应该把受伤的同学送到附近的医院去打一针。被带铁锈的东西划伤感染破伤风的概率是多大，大家都知道。一旦感染那基本上就没救了，还好北航福大、我同学命大，要不然北航就梅开二度了。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;当我看见首都新十大建筑的50个候选建筑里面有北航新主楼时，我为我是一个北航人深深的感到骄傲。50候选里面只有两所学校的建筑入选了，而另一座就是矿业大学的宿舍楼，我的骄傲中充满了苦涩。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;北航的宿舍条件是最差，当我还在上高中的时候北京市各个高校为了改善学生住宿条件纷纷新建校区或者盖大型的单体宿舍楼，一时间很多学校都能看见粉红色的塔楼拔地而起，什么公主楼，王子楼，恐龙楼，青蛙楼……而那时北航在做什么？博彦大厦，世宁大厦，新主楼，唯识大厦……全tm是写字楼，这就是人文的关怀？人家先修好一座楼之后先把所有女生装进去，而北航女生还在住最破的楼，一座是简易楼，一座连稳定的220V电压都不能保证。我的同学还在住他爸爸上北航的时候住的楼，像这样的宿舍北航还有不少于10座，在很多大学基本完成国家4-2-1计划时，北航多数本科生还是住8人间，北航没钱吗？那么多漂亮的写字楼盖德起，先进的办公设备买的起，听说北航办公楼的一扇门作价是200000，跟中石油2000万德等有一拼了。一说出去某某某学院连续几年研发经费过亿，某某某学院过亿。就是没钱盖宿舍。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;当北航新主楼以全亚洲最大的单体教学楼自居的时候，对外经贸大学建了全亚洲最大宿舍楼。当你在北航教学区为了买瓶水而不得不走出几公里时，地大新落成的宿舍楼最下面几层就是教师，学生在恶劣天气里上课自习都不用出楼，而在北航在最恶劣的天气里学生要在冰天雪地里走上十几分钟，而大教授们可以直接把车开到新主楼的地下，上课的时候还会抱怨为什么来的人那么少，你看人地大出勤率多高。<br />
<br />
