													<h2>北航甲流新生死亡真相(转)</h2>
						
						
																											<div id="postmessage_305931" class="t_msgfont">我。一个刚从北航大兴军训基地逃生的学生关于这次军训闹剧的回忆。。<br />
<br />
<br />
<br />
首先声明的是。。我很健康。。并已经签署了自行在家隔离一星期的协议。。就如同昨天晚上拼死往家跑的所有北京学生一样。。到家之后听到外界的评论仍然感到自己是刚刚从地狱脱逃。。现在就让我静下心来。。给大家讲一讲这次惊心动魄的军训<br />
<br />
<br />
<br />
首先。很明显。。我们是为了给挑战杯腾出宿舍。。我已经不想骂人了。。入冬时节！赶走大一新生！一人在基地死亡！千余人发烧！<br />
<br />
<br />
<br />
回想起刚进入军营的那天。我们怀着满腔的愤怒。。。走进了大兴军训基地。。看到那单薄的平房。。光光的木板床。。大公厕在宿舍外一百米处。。不是人吃的饭菜。。早餐有时还会发凉的馒头。。就想到这次军训不会有什么好的结果。。但也没想到会演变到现在这种地步。。<br />
<br />
<br />
<br />
就不说军训有多苦了。。军训都是。。但是咱不能有点人文关怀吗。。甲流时期，又是因为给挑战杯腾地方被赶出来，这么冷的天不能加点被吗！？不能不让我们六点多起来出早操吗？不能加点饭菜吗？好吧。。哥们咱是工科学校。。可能挑战杯比较重要。。但是！我看你怎么但这么大的责任！】<br />
<br />
<br />
<br />
前两天很苦很累。。已经陆续有人倒下。。当天晚上能洗澡了。。他给我们一个连180人加起来在一个破的通风小澡堂十分钟的洗澡时间。。十分钟！！水还基本不能被称为热水！！冷风对着里面吹着！<br />
<br />
<br />
<br />
到了军训的第三天。。累积的发烧人次估计已经达到一百多人。。一百多人！！！我们班呢？三十八度四。三十八度八。三十九度四。三十九度六。。。。这种数字已经开始闹得我们班人心惶惶。。上铺倒下了。。同床倒下了。。一个不足十五平方的房间住了26人！在甲流盛行的时期，这么拥挤的坏境不出事就怪了！。。。但是。。军训还在继续。。教官有的也烧到了三十九度。。但是还在继续。。有的教官还说什么在队列里别给我咳嗽。。。你们他妈怎么想的。。让你站着军姿睡觉你行吗？还好我们那个教官人很好。。对我们很好。。。这我很感激。。但是！军训还在继续。。<br />
<br />
<br />
<br />
说说第四天。。我们班已经8人倒下。。三十人的大班已经8人。。三千人的军训团呢？我才不信媒体说的数字。。这个时候。。那的医务室已经不行了。。门口总是十几二十人排着长队。。里面的医生抱怨人怎么这么多。。我对里面医生看病的质量不做评价。。这个时候。。军训依然没有停止。<br />
&nbsp;&nbsp;到了第五天。。估计也是最悲惨的一天。。我们班报数已经由一开始的“1.2.3.4.5.6.7.8却2满伍”变为“1.2.3.4缺2满伍”。。兄弟们都倒下了。。这一天。。训练终于基本停止了。。而隔离病房已经满了。。已经有同学开始转院。。整个军训基地笼罩在一片阴霾之中。。辅导员倒下了。。空军总站的车来了。。工讯部长来了。。拉起了警戒线。。禁止与外界联系。。宿舍门口修起了铁门。。而我们。。却仍然一无所知...接近一千人发烧这个可怕的数字。。然而最过分的是。。在当天晚上十二点。。由同学们把还在宿舍的发烧的人送到隔离区。。这天工讯部长给我们送来了特效药达菲。。还算有点人情味<br />
<br />
<br />
<br />
第六天是真正的灾难。。白天没有什么事。。到了晚上。。恐慌爆发了。。就有我们宿舍说起。。导员进来了拿着个电话给我们一同学让他接家长电话。。。接了之后他跟我们说。。北航军训团28例甲流。。死了一个。。我们当时一下炸开了。。死了一个？？？不可能！我当时就这么跟自己说。。。各个宿舍开始议论。。。营房外大家好像都知道了消息。。奔走相告。。当我们从导员口中获得官方声明后。。大家蜂拥向服务社。。顾不得纠察。。大家都互相借电话卡。。站在那里唯一的一个能用的IC电话亭排队（其他的线路都被切断了。。在之前还禁止打电话。。我们军训禁止带手机）。。自己偷着带手机了的都把手机拿出来大家在营房门口排起了队。。这是大家已经人心惶惶。。当听到有北京的孩子已经被接走时。。所有北京的同胞都马上寻求各种联系方式要求家长马上到大兴军训基地。。就这样。。外地的孩子也慌了。。有人竟对着电话大喊“你来不来北京？！你是不是不要我了？！”。。<br />
说实话。。当时看到这样的场景。。是谁都会咒骂起来的。。而我。。要不是知道死人的消息。。就算有甲流也不会主动提出撤退的。。而这次。。实在太血腥。。从同学拿得到的消息是。。那个人。。<br />
5系的。。并不是死在了医院。。是死在了我们基地。。是人们看着用黑色塑料袋包着抬上救护车的。。听到这。。试想一下。。你是否想要离开这样一个鬼地方。。这样的军训。。为了挑战杯。。<br />
一个刚刚考上大学什么还都没学到、对大学生活满怀憧憬的大一新生就这样离开了。。我走之前。。。去服务社买了一些吃的和纸牌留在宿舍。。有些不舍又有些急切的离开了营房。。往门口走着。。谁知道明天我身后这扇铁门会不会封住我们的营房。。不想再说太多了。。一切就是一场闹剧。。<br />
（惨剧。）（请看其二。）<br />
<br />
<br />
<br />
北京航空航天大学，一个刚刚经历了自己五十七岁生日的大学，在追求世界一流的道路上，可能这两天戏剧般地经历了其间一个必须的过程――世界知名！<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; 10月22日，北航零九级新生赴大兴基地军事训练，23日晚部分同学已经开始出现感冒发烧现象，而且人数增加很快！目前报道为28例甲流，1例死亡（死者成为中国第四个死去的甲流患者）。10月29日，军训基地正式开始封闭。至于他们什么时候会放出来，我倒想起了《边城》里那句话“也许明天，也许永远也不。”生命有时候真的就这样脆弱，一个花季少年已经就这样离开了我们，所有的希望，瞬间成为泡影！作为同样经历了那么多年奋斗的一名学生，我更愿意称他为我的一名战友，战友的离开除了让我们悲痛，更应该让我们懂得珍惜身边的所有，更要珍惜自己！<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; 我作为一名大一新生，成为为数不多的能从基地撤离在外隔离的一个，心里已慢慢平静，可想到已经有我的一名战友死去，想到还有那么多同学仍然呆在那个根本不是人住的地方（8人，16人，25人……的房间），接受所谓的隔离，心情又不免激动，所以，我想说：“校领导，……你们母亲的！”<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; 我知道现在骂人是没有用的，所以我会尽量地控制自己的情绪，来写完这篇让大家能了解我们军训真实情况的文章。因为我觉得，在这个问题已经不仅仅是道德层面上，更应该上升到法律层面上的时候，把真相公布出来，让所有人知道，是对大家的尊重，更是对我死去的战友，我仍然在接受封闭的同学的尊重！<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; 我们22号下午来到大兴高校军事训练基地参加军训，这里的条件很不好，周边是公路，比较荒凉的地带，而基地内部又只有一个不大的军人服务社（来之前已经三令五申不让带钱，可为了防止一些带钱的同学买东西，还是布了层层重兵，严加防范，一个个的纠察趾高气昂，仿佛抓到谁买东西就要立刻拉到厕所淹死在粪坑里似的）。基地最大的特点就是没有几个楼，据我的观察，只有两个(不能用“幢”来说)楼，这里住的大部分女生，而男生都住在板房区。这板房就是在电视报道上看见了无数次，给四川灾民们送去温暖的板房，可它只给我们带来的寒冷和疾病！北京的深秋，十月末，已然一片萧索，许多叶子还没有机会变黄就被寒风打落了，早上和晚上只有不到十度的温度（我曾光荣地成为站岗的学生，从半夜1点到2点，穿着背心，长袖棉T恤，迷彩服，大厚棉军大衣，已然在寒风中冻得乱蹦），板房的隔凉效果很次，所谓的墙薄到“宿舍间隔墙聊天”的效果！到了晚上，板房里的温度可想而知，根本不能脱衣服睡觉，许多同学就是从去了开始衣服就是白天晚上地穿，根本脱不下来，就更别提开窗通风睡觉了！而这，也为疾病的传染提供了好的条件。不知道为什么，军训基地的同学都发现了个问题，这里的蚊子和苍蝇特别特别地多，也不知道它们怎么有这么顽强的生命力，能在这种温度保持这样旺盛的生命力，还是听说我们要来一直死死支撑着，等待这一批新鲜的血液！军训的卫生工作做的也非常差劲，苍蝇蚊子乱飞，而且每天训练完吃饭连洗手都没有安排，可以说是训练完就集合去吃饭，米饭一般每人只能分到2两，吃不饱就吃馒头（早上馒头，鸡蛋，咸菜，红方，姜汤……中午米饭吃不饱吃馒头……晚上米饭吃不饱吃馒头……），用那样的手拿着馒头（有的馒头还是很脏的，百分之一万是掉到地上脏了直接捡起来就发下来的！），脸上浸着还没有干透的汗液，面对着已经不能再难吃的菜，每个同学还是吃得很满足，因为他们已经没有别的选择！恶劣的卫生条件更是加剧了疾病的传播……<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; 从23号晚上起，发烧渐渐开始出现了，当天晚上光我听说的，我们连发烧到38度以上的同学已经达到了5，6个，有感冒症状的更是没法数！一共十六个连，总数可想而知……这样一个高调的开始，也注定了一个不平凡的过程！基地有一个小型的疾病临时处理诊所（我实在不能昧着良心像某些人称它为“校医院”，否则都对不起我家小时候邻居家爷爷的外甥的单位门卫他儿子养的那条大黄），开始有一个标准，发烧到38.5度以上的同学送到大兴医院救治，可随着人数的增多，标准提高到了39度，到26号晚上，连39度的都也已经不送了，就是给打一针让回寝室休息！就这样的管理，在这样的寝室条件，怎么能康复，怎么能不传染！！人数一天天增多，到26号为止，光是发烧到38度以上的已经至少达到400左右，随时到所谓的诊所去看病都要排长长的队伍，这里已经不是个军训的地方，倒像是个集中营。军训先是大量地减训，大概是为了稳定军心，充实生活，象征性地训一两个小时，后来干脆就不训了，自己休息，再后来干脆把教官就拉走了，军训彻底他妈的黄了（对不起，此时情绪已经不能抑制了，稍微爆发一下）！！军训基地大部分人都是戴着口罩，来回踱步的，气氛十分压抑。据报道说什么71人发烧到38度以上，我不知道真假，反正我是从军训基地里出来的，就根据我说这个情况，如果27号38度以上的只有71个，你可以自己算下大家康复得有多快，你可以马上振奋地拍脑门说：“就这医疗水平咱们他妈的癌症再有一年就攻克了！ <br />
<br />
&nbsp;&nbsp;27号，他走了。<br />
&nbsp; &nbsp;&nbsp;&nbsp;我们在基地的大部分还都蒙在鼓里，我就不信这时候甲流爆发还没有消息，只能说我们这么大的一个国家保密工作做得能这么好真的是不错！北航的学校教育太成功了，国家安全法教育，保密教育进行的十分到位，校领导也在这关键时刻用他们的行动为我们又上了一课！……他们母亲的！！！<br />
&nbsp; &nbsp;&nbsp; &nbsp;甲流就这样，化验成阳性的结果出来以后，仍然又蒙蔽了两天（结果26号肯定就出来了，28号晚上才公布，大概是因为尸体已经停留了一天没有公布发臭了吧！还有小道消息称那学生就是死在了军训基地，单从基地对高烧39度以上同学的对待方式看，我真说不出来“不可能”这三个字，现在只有等进一步的调查让事实大白天下了），我们在基地的同学仍然是与世隔绝，还在幻想着挑战杯结束回学校会餐，这梦想在现在看来可能更加遥远了吧！28号晚上，我们听说29号开始要正式开始隔离，“正式 隔离”！当时我们对“正式”的理解可能还没有到位，可能还不确定我们是因为肆虐的甲流还是单纯由寒冷引起的流感隔离，可能还不确定我们要封闭到什么程度，是否还是原来那样，只不过是不能出去军训基地的大院……可去吃晚饭的路上，我们明白了，这是真格的了，因为已经有人在焊接我们居住区外的大铁门！！！如果铁门关上，我们剩下的活动区域只有寝室所在的院子一点点了，我们不知道他们是不是还要把寝室的门也一起焊上，然后在窗户上开个20厘米见方的口给我们送些东西什么的！冷风又刮起来了，我们似乎看见了铁门锁起来的样子，看见了外面的一个六斤半大锁，两条大狼狗……哇塞，太TMD酷了，跟电影里监狱一模一样的！！！<br />
&nbsp; &nbsp;&nbsp; &nbsp;我们要越狱！！！<br />
&nbsp; &nbsp;&nbsp; &nbsp;终于，28号晚上，确切地说是29号凌晨，我从大兴训练基地里逃了出来，理由是在外隔离，当然，要有家人或者亲戚来接走。我是和同学一起走的，连蒙带骗，全寝六人齐心协力都走了，到北京的两个同学家里重新开始了“人”的生活！吃到了饺子，包子，洗了个澡，如唐僧取得了真经一般！<br />
