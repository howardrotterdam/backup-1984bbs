我们是大一新生，对北航制度都不大了解，但是通过各种渠道我们知道这次军训是有史以来第一次在深秋开展军训。这个时期温差大，多风，本身就是流感的高发时期，又恰逢全球甲流爆发，在最不该军训的时候我们被要求去军训，这是为什么呢？很多人已经在网上回答了这个问题：北航要搞挑战杯，为了节约住宿经费，我们大一新生得腾出房间好让这些精英们住。刚刚搬到北航不到一个月，辛辛苦苦把东西安排的基本妥当的时候，要求我们在一天时间内把所有东西收拾好，嗯，校方说我们很光荣，嗯，但是我们很辛苦。于是我们收拾好了宿舍，跟我们刚来的时候一样，然后拖着部分行李去大兴赴训。 <br />
<br />
感冒是在我们军训的第三天开始爆发的，当时据我的去基地诊所的舍友说，每隔几分钟就有一个高烧在39度以上的，38度到39度的也不少。但是这件事并没有引起校方的重视。我们每天在那里有七个小时左右的训练，偌大的操场上我们必须身着别人在夏天军训时的服装，穿着只有一层薄布厚的劣质鞋，在寒风刺骨的十月底承受着痛苦。于是，越来越多的人感冒，什么70+个在38度以上的，全扯淡。大兴基地进门有2座3，4层的宿舍楼，起初是给女生用的。但随着发烧人数越来越多，校方担心向医院输送大量高烧学生会引起外界怀疑，于是先是将两栋楼的顶层变成了隔离区，在那段期间，每天有N多同学上去，他们几乎得不到任何医疗措施，你想想一个只有不到50平米的小诊所如何能提供给这么多人的医疗保障。到军训的第四天和第五天最顶层已经人满为患了，但是我们的训练仍在继续！发烧人数一直在上升，我们的一个严重的连190人只剩下90多人，其他人全都被隔离了，但是我们有多少个连?16个！很多学生的家长来到基地，虽然我们不知道他们是怎么了解的，因为我们在军训前已经将手机全部封存了，根本不让拿，我们无法跟外界联系，无法向我们的父母报平安，但是我们都知道事态已经很严重了。在第6天终于停止了训练，军官们全部撤离，只剩下我们3000学生和一批辅导员空守在这个所谓的军训团，第6天下午女生开始搬离宿舍楼，因为顶层装不下了，所以得用两栋楼来装，回来之后我才知道那天就是27号，我们的一个弟兄远离我们的日子，所以校方终于采取行动了，开始上报给上级机关了（在此之前基本一直对外隐瞒不报）。于是一辆辆的大巴驶入基地，先送走的都是39度以上的，38度的都不好意思上车！整个基地最少最少有400人发烧！感冒的更是没法统计，我们连算是疫情控制的不错的，但我们宿舍8个人就有5个人得了不同程度的感冒。到了第七天，也就是昨天，两栋楼也装不下了，在我们活动板房的后面又开了3排的板房隔离区！疫情就在我们身边！只有亲身经历才能感觉到那种恐怖！一些同学私自带的手机成了所有人的救星，排着队给家里人报平安，我们也是在那天通过跟家长联系了解到一哥们永远离开了我们。我们能说什么？我们有恐惧！但我们更多的是愤慨！！ <br />
