<br />
读罢让人不禁潸潸！<br />
<br />
首先本人再次证实北航因为要举办挑战者杯决赛为选手腾出宿舍而撵大一学生去军训这一消息。当时此决定一出，北航在我心中本已不佳的形象再次趋于无限BS。在清华9月军训时，清华新生就因为天气或病毒流感原因发烧超过38度者几近1/3（从我系观察来看），发烧超过39度需要隔离观察者也数见不鲜。在校医院时，校医曾私下对我说，作为校医她最反对大一军训，首先大家刚休息3个月，体质跟不上，其次很多人刚来到北京对北京的气候不适应，再加上有些人想家或没有朋友很容易造成心理不适，这些都能导致大家免疫力下降，发高烧在所难免。而现在已十月下旬，秋天气温更加多变，穿着劣质作训服根本不能保温！再加上季节性流感与H1N1叠加，我十分坚信以上同学自述中关于发烧人数的记载。<br />
<br />
其次，作为9.24-10.1在大兴高校军训基地进行过高强度特殊训练的学生，我想对基地情况进行下补充。我基本证实以上报道为事实。女生宿舍条件较好，在此不做评述。全体男生都住在一个叫简易板房区的地方，分8人寝，16人寝，32人寝。宿舍完全不能保暖，内部仅有木板床，需学生自己带床褥。当时我们统一带一条褥子与一床被，尚可度过并不寒冷的夜晚。而当不知情的北航同学也带同样床褥的情况下，可以想象，其是怎样度过这几个难熬的夜晚的。但寒冷不是最大的问题，是潮湿！魏善庄地处远郊，经常下大雾，一觉起来被褥已湿，在这样的情况下如何叫病号好好休息？还有饮食，那个1234#食堂就像那个万人厕所一样在我心中留下惨痛烙印。由于我清华新生参加国庆特殊任务，享受每日50的伙食标准，按理说应十分不错，可是每天除了一头头猪被宰后不同部位放进我们面前的各个盆子里，我们看不见任何绿！色！蔬！菜！还好有各级领导带来的一些水果可以暂且补充一下维生素。那么，北航的新生们呢，他们在享受不到如此“高”的待遇的情况下，伙食如何难以想象。<br />
<br />
我想说：不拿自己的学生当自己孩子的教育者，应该被斩首！揽天下英才而教育之，不亦乐乎。教育者们这样说时，和当年李世民看到全天下贤士都到长安赶考时说的那句XXXX尽入我裤裆大概是一个心理，有一种被认可的满足感。可是大家看看他们对从全国各地通过12年的努力和高考的博弈进入高校的学生们都做了什么！在北京新闻中看，那个哥们死在大兴医院。（有的版本也说这位同学死在基地，包着黑塑料布抬出去的，仅供参考）从基地到海淀只有两个小时车程，而在这个学生危重的情况下校方却选择将其送往一个郊区医院，每个医院都说经全力抢救无效，可是一个郊区医院和一个三甲医院的抢救手段能一样么？！从上面同学的介绍中来看，学校已经组织学生在吃达菲。达菲作为抗击甲流的特效药，这已经充分说明校方已经意识到基地甲流流行却选择让学生私自服用达菲妄图将疫情掩盖下来。时至今日，在北航依然有学生不知道他们的新校友已经死于基地的消息。那个身居高位，在挑战者杯开幕式上满脸堆笑宣布比赛开始，享受来自全国同样青春的学子热烈掌声的人，请问你看到无数父母苍老的双眼发出的灼灼目光在戳你的脊梁吗，你听见那些把中国的希望寄托在学生身上的人的扼腕叹息吗？<br />
<br />
听见北航在大兴挂人了，很多校长偷笑了。在这几个月里，首经贸、北京化工、清华很多高校都在这里训练过，而最终北航出了问题。请你们在庆幸自己免受镁光灯的拷问的同时，真正的做到教育者的本分，给中国的未来一个交代。<br />
<br />
让我们再次为逝去的同学默哀。<br />
