<br />
第一件坏事：谢某人竟然是个贼，犯过“监守自盗”的事儿。这是从北大信管系那里获得的，当年他在该系图书馆专业任教，伙同北大出版社发行处的副主任将新出版的书，以处理废纸的方式盗出，拿到谢某在海淀图书城开的小书店出售。大约1997年东窗事发，给北大出版社造成了严重损失，那位副主任被开除、罚款(北大考虑到影响，未追究其法律责任)，当时海报张贴在三角地的橱窗里。<br />
<br />
这件坏事对谢某的影响：仅限于免去其系团委副书记的职位。而且之后在2001年新闻学院成立时，谢某又谋行政职位，他当时表现得“极勤恳”、常汇报(每天给领导们一天十多个电话)、肯吃苦(最形象的表述是太阳底下扛桌椅)等，获得副院长候选人资格，并用在新闻学院未成立时“透支”了第一个教授指标，升为教授。此时，信管系的领导向组织部门重提监守自盗的事，组织部门要求新闻学院严格要求他的同时，同意他担任新闻学院副院长。<br />
<br />
第二件坏事：谢某人的博士论文竟然由3位硕士生代笔。谢某是在升教授后回信管系读博士学位的，导师其时已在新闻学院任教，2004年获得博士学位。他的论文是关于网络传播的内容，9章，由3位他的硕士生各写3章，当时的学生都知道。事发于2004年二学位一毕业生的揭发，这位学生因为毕业论文有抄袭行为而被处分、未获得学位证书，他愤愤不平，因此投书北大的各部门举报了谢某。<br />
<br />
这件坏事对谢某的影响：博士论文获得2004年北大优秀毕业论文。那篇关于网络传播的论文，是在信管系答辩的、答辩老师都是由谢某自己邀请的，其中没有任何一位新闻或传播的专业教师，也未公开答辩的信息。他向包括关系密切的新闻学院领导在内的所有人，都保密了他的答辩一事，而在信管系顺利过关，并通过公关由系里推荐为北大优秀毕业论文。那时学生的投诉信已经投到纪检等部门，但研究生院通过了其“优秀论文”。如果各位能够到国图、北大图书馆去查询一下这位仁兄的论文，就知道什么是北大“优秀论文”了!我转折看到，也立即赞同内行专家的意见：一篇并不流畅的拼凑文章，通俗文章都算不上。那3位代笔的硕士生据说确实很牛、比谢某强多了，其中两位是被外派到美国交流期间代写论文的。谢某胆子大到敢宣称自己是所有最热门专业(最可获利的)专家--在还未学会打字、不会上网的情况下，2001年领导说要重点发展网络传播时，就立即向领导说明是搞“网络传播”的、要求上“网络传播”的课，结果把学生都上跑了!据所有采访到的学生言，谢某的课是新闻学院“最最烂的”―因为还有“最烂的”，学生评分总是最低的，而且极不负责，经常找个学生或外行来代课，曾经把数十学生上跑、只剩下5人!但由3位硕士生匆忙攒出来的论文，你想想会优秀到什么程度吧!<br />
<br />
第三件坏事：谢某竟然将学生交给他的课程作业偷偷发表，遂成“剽窃门”。此事发生在2005年末。对这件事的了解，更费周折些，我采访了许多人(抱歉有时是通过聊天的方式获得)。2005年春季学期某硕士研究生交给谢某一篇期末论文，在年末时偶然在网上发现竟然被发表了数月了，分别是复旦大学的一次国际学术会议发表、新华网上发表、列为谢某学院简历中一篇“研究成果”。该学生已经将此作业翻译成英文提交给美国的大学、正在申请美国的学校，她很害怕美国学校把自己当作剽窃者，于是心惊胆颤地检举了谢某。<br />
<br />
此坏事对谢某的影响：北大学术道德委员会组成了联合调查组，开了数轮会议，后来此事与新闻学院的换届等事交织在一起，北大又担心出丑闻，便动用种种力量，通过中宣部封锁了所有媒体，不允许报道。2006年北大以“分阶段处理”的方式，暂缓作结论，至今未结。此时谢某利用种种公关手段化解，并散布谣言将这件确凿的剽窃事件当成“内部权利斗争”。在此事未作结论的情况下，2006年夏谢某竟然升为博导! 学校要求新闻学院对此坏事有所体现，于是学院在未有任何通告、会议的情况下，仅限于悄悄抹去谢某的学院学术委员委员。这件坏事新闻学院的师生都知道，但都是从非正常渠道获悉，学校、学院未有任何公开的会议宣布过、提起过。据一位跑教育口儿的同行说，新闻学院某领导一直努力要恢复谢某的学术委员会委员一职―这是剽窃事件惟一的后果，如果恢复，就没有任何后果了!<br />
<br />
最令人恐怖的是，谢某的得志事之中的几件，如升教授、升博导、升副院长、获优秀论文奖，都是伴随着坏事而成的!<br />
<br />
上述三件坏事、风暴，未对谢某产生什么后果，都是因为北大这棵大树把他给掩盖了、遮蔽了，而北大无论领导是谁，有一点是不变的：掩盖所有的丑闻。谢某已经深谙其道，因此越发嚣张跋扈，简直无所顾忌―试想，那么大的事，搁在任何人身上都会半死，但他却风生水起!<br />
<br />
网上说谢某支使跳楼而亡的学生贾昊免费为其儿子做家教，许诺他继续攻读博士却出尔反尔(没有考试的情况下就许诺硕、博，据说谢向来如此，并真的成功，考试只是走走过场，不知情的其他考生就是这么落选的)，一贯如此使用自己的研究生，让学生给他干各种各样的杂活、视学生为奴役……这些信息，我向学生们和老师们求证，他们甚至笑着说“地球人都知道”!<br />
<br />
<strong>是谁保护了谢某的嚣张跋扈、为所欲为，并由此引发终于逼死学生的悲剧？是北大这棵大树。这棵大树，其实就是中国最著名学府北京大学的制度。<strong><br />
<br />
