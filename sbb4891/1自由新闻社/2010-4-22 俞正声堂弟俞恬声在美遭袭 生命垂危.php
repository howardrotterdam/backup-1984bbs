																											<div id="postmessage_577582" class="t_msgfont">二十世纪中国最显赫的家族<br />
<br />
 　二十世纪中国最显赫的家族是哪一家？蒋家？宋家？毛家？邓家？江家？都不是。二十世纪中国最显赫的家族是绍兴俞家。历经百年而不衰，英豪俊杰辈出不穷，政界、军界、学界、商界全盘打通，这样的家族惟有绍兴俞家。<br />
<br />
　　俞家到底出了哪些人呢？还是先从大家比较熟悉的现任中共中央政治局委员，中共上海市委书记俞正声说起吧。<br />
<br />
　　俞正声，男，汉族，1945年4月生，浙江绍兴人，1964年11月入党，1963年8月参加工作，哈尔滨军事工程学院导弹工程系弹道式导弹自动控制专业毕业，大学学历，工程师。<br />
<br />
　　现任中央政治局委员，湖北省委书记。<br />
<br />
　　1963－1968年　哈尔滨军事工程学院导弹工程系弹道式导弹自动控制专业学习<br />
<br />
　　1968－1971年　河北省张家口市无线电六厂技术员<br />
<br />
　　1971－1975年　河北省张家口市桥西无线电厂技术员、负责人<br />
<br />
　　1975－1981年　第四机械工业部电子技术推广应用研究所技术员、工程师<br />
<br />
　　1981－1982年　第四机械工业部电子技术推广应用研究所副总工程师<br />
<br />
　　1982－1984年　电子工业部电子技术推广应用研究所副所长、电子工业部计算机工业管理局系统二处处长、副总工程师兼微型机管理部主任、电子工业部计划司副司长<br />
<br />
　　1984－1985年　中国残疾人福利基金会负责人、副理事长、党组成员（其间：1984年12月明确正司局级，1985年1月－3月康华实业公司代总经理）<br />
<br />
　　1985－1987年　山东省烟台市委副书记<br />
<br />
　　1987－1989年　山东省烟台市委副书记、市长<br />
<br />
　　1989－1992年　山东省青岛市委副书记、副市长、市长<br />
<br />
　　1992－1994年　山东省委常委、青岛市委书记、市长<br />
<br />
　　1994－1997年　山东省委常委、青岛市委书记<br />
<br />
　　1997－1998年　建设部党组书记、副部长<br />
<br />
　　1998－2001年　建设部部长、党组书记<br />
<br />
　　2001－2002年　湖北省委书记<br />
<br />
　　2002－2003年　中央政治局委员，湖北省委书记、省人大常委会主任<br />
<br />
&nbsp; &nbsp; 2003－2007年　中央政治局委员，湖北省委书记（至2007年10月 ）<br />
<br />
&nbsp; &nbsp; 2007年－&nbsp; &nbsp;&nbsp; &nbsp;中央政治局委员，上海市委委员、常委、书记<br />
<br />
&nbsp; &nbsp; 第十四届中央候补委员，十五届、十六届、十七届中央委员，十六届、十七届中央政治局委员。<br />
&nbsp; &nbsp;<br />
　　细心的朋友会发现，俞正声在电子工业部时，正是江泽民任电子工业部部长的时候。（江泽民1982年后，任电子工业部第一副部长、党组副书记，部长、党组书记。1985年后，任上海市市长，中共上海市委副书记、书记。）而俞正声到中国残疾人福利基金会任职时，正与邓朴方同事。（邓朴方1983年创办中国残疾人事业。1984年3月任中国残疾人福利基金会副理事长、理事长。1988年起任第一届中国残疾人联合会主席团主席，执行理事会理事长。）这应该是老邓的刻意安排，让他去助儿子一臂之力。邓朴方毕竟缺少资历与磨练，需要一个得力的干将来扶助他。<br />
<br />
　　俞正声的曾祖叫俞明震，俞明震（1860―1918），字恪士，号孤庵，晚清知名于诗界、教育界、政界。甲午战争时，曾协助唐景崧据守台湾，还曾担任厘捐总局局长，甘肃省学台、藩台等职。并曾担任过南京江南水师学堂督办，也即校长。1898年，十八岁的鲁迅进入该校，成为俞明震的学生。《鲁迅日记》中多次提到“恪士师”，就是俞明震。<br />
<br />
　　俞明震娶曾国藩的孙女，曾纪泽（曾国藩次子，曾出任驻英、法公使，后又兼充驻俄公使，与沙俄谈判修改《里瓦几亚条约》，名闻一时，有爱国之声，为中国第一代外交家）之女为妻。<br />
<br />
　　俞明震共有三兄弟，他居长。俞明震的三弟（名字？）有个儿子叫俞大维，在台湾国民党政府当过国防部长。俞大维的儿子俞扬和后来和蒋经国的唯一女儿蒋孝章结婚，生了个儿子叫俞祖声（和俞正声同辈），现在是蒋家第四代中的重要人物。而俞大维之妹俞大彩，则是北大校长傅斯年的夫人。<br />
<br />
　　俞明震有个妹妹嫁给陈三立（这个陈三立就是维新派主将陈宝箴的大哥），生下3个儿子，老大陈衡恪是画家，老三就是一代国学大师陈寅恪。<br />
<br />
　　俞正声的祖父叫俞大纯，又名俞大线，是俞明震的长子。俞大纯膝下四子两女，长子俞启孝生于北京，留学美国，回国后在天津当教授；老二俞启信生于德国，专攻化学；老三即俞启威（后来改名黄敬），生于北京，人称“三少爷”；老四俞启忠，学农，从美国回国后在北京当教授；大姐俞珊，生于日本，演员。小妹俞瑾，一生从医。<br />
<br />
　　俞大纯的三儿子俞启威（黄敬）就是俞正声的父亲。俞启威20世纪30年代初在青岛大学求学期间，认识大学图书管理员李云鹤。俞启威（黄敬）介绍李云鹤入党。两人后来同居，之后又分手。这个李云鹤便是后来的江青。俞启威（黄敬）后来与著名历史学家范文澜（也是绍兴人）的妹妹范瑾结婚，范瑾曾任北京市委常委兼北京市副市长、《北京日报》社社长。1949年1月，天津解放，俞启威（黄敬）被任命为天津市第一任市长兼市委书记，1952年调北京出任第一机械部部长。1958年因病去世，年仅46岁（传说他因被毛泽东当众训斥，惊吓过度成精神病而去世）。<br />
<br />
　　俞启威和范瑾育有二子：长子俞强声，次子俞正声。俞强声毕业于北京国际关系学院，毕业后分配到北京市公安局，1983年设立中国国家安全部时他被选调进入。1985年，时任中国国安部外事局主任的俞强声叛逃到美国。这是中华人民共和国历史上最严重的四次叛变事件之一，也是四次中最早的一次。由于俞强声的叛逃，导致当时的第一任国家安全部部长凌云被解职，国安系统不得不予全面换血。俞强声叛逃到到美国后又名俞真三，后来到南美某个国家去旅游时失踪。<br />
<br />
　　俞正声之妻张志凯，为前国防科工委副主任张震寰之女。<br />
<br />
　　叶剑英元帅的夫人（第二任？）曾宪植，是曾国藩的曾孙女（应该是曾国藩弟弟的曾孙女？）。曾宪植得管俞正声的曾祖父俞明震叫姑父，与俞正声的祖父俞大纯是姑表兄妹。换句话说，俞正声的父亲俞启威（黄敬）得管叶帅叫表姑父。<br />
<br />
　　俞正声的父亲俞启威（黄敬）1952年至1958年任一机部部长期间，江泽民从原不属于一机部的上海制皂厂第一副厂长调入一机部上海第二设计分局电器专业科任科长，1955年又被派往苏联莫斯科斯大林汽车厂学习，1956年回国后，任长春第一汽车制造厂动力处副处长、副总动力师。而李岚清是1952年至1956年任长春第一汽车制造厂计划科计划员、副科长，1956年至1957年赴苏联莫斯科利哈乔夫汽车厂、高尔基汽车厂实习，1957年至1959年任长春第一汽车制造厂计划处科长。<br />
<br />
　　俞正声今年65岁，连任十七届中央政治局委员。而他的后代情况也少有报道，但他能在自己的父亲与江青有特殊的关系，并且过早去世的情况下，安然度过文化大革命；在自己的亲哥哥叛逃出国、不知所终的情况下，节节攀升，其能量的确无人能出其右。这种能量，除了个人的原因外，其家族的力量绝对不可低估。</div>

							
							
							
							
