黄敬，原名俞启威，是浙江绍兴县一个世代官僚家族的后代。 <br />
俞家是浙江绍兴的名门望族。黄敬的祖父俞明震（１９６０―１９１８），字恪士，号孤庵，晚清知名于诗界、教育界、政界。甲午战争时，曾协助唐景崧据守台湾。俞明震还曾担任厘捐总局局长，甘肃省学台、藩台等职。俞明震曾担任过南京江南水师学堂督办，也即校长。１８９８年，十八岁的鲁迅进入该校，成为俞明震的学生。《鲁迅日记》中多次提到“恪士师”，就是俞明震。鲁迅还曾在《琐记》一文中，以亲切的笔致描述过后来送他出国留学的“恩师” ,他坐在马车上的时候大抵看着《时务报》，考汉文也自己出题目，和教员出的很不同。有一次是《华盛顿论》，汉文教员反而惴惴地来问我们道：“华盛顿是什么东西呀？……” <br />
