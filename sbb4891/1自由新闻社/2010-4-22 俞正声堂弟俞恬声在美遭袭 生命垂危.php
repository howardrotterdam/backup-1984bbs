<br />
俞正声之妻张志凯，岳父并非外界所传的是前国防部长张爱萍，而是前国防科工委副主任张震寰。 <br />
俞正声与邓朴方的关系友好，他在80年代初期邓朴方请他出山担任&quot;康华&quot;公司总经理（大概在80年代已经成年的中国人都知道康华公司的来历与背景），与邓家关系非同寻常。邓小平原来准备安排他为中共中央书记处书记、中央统战部部长，利用其个人关系去搞台湾关系。但俞正声在92年十四大时意外落选中央委员，而原统战部长丁关根已经改任宣传部长，不得不提拔王兆国任统战部长，而王兆国本缺台办主任又没有物色好接班人，只能让王兆国再兼任两年台办主任。 <br />
<br />
1952年秋天，重工业部撤销，分别建立第一机械工业部、第二机械工业部、冶金工业部等。汽车工业归一机部领导。一机部部长即是黄敬，一机部建立汽车工业管理局，张逢时任局长，江/ZE/民即任副局长。 <br />
<br />
1953年7月15日，位于长春的一汽举行奠基典礼，参加典礼的有东北人民政府第一副主席林枫、第一机械工业部部长黄敬等。会上黄敬讲话，李岚青、王恩魁等六名年轻共产党员，将刻有毛主席题词&quot;第一汽车制造厂奠基纪念&quot;的汉白玉基石，放置在厂区中心广场基座上。可见对江core和李岚青这些老机械、老汽车来说，俞正声的父亲黄敬一直是他们敬重的老领导。 <br />
<br />
综上所述，这个家族关系要是写一本书绝不为过。 <br />
近现代中国史上，除了宋氏家族以外，君见过如此复杂深厚的关系吗？囊括了江青（毛泽东）、蒋经国（蒋介石）、曾国藩、陈宝箴、陈寅恪、邓朴方、江core、李岚青这些我们还不知深浅的交情</div>

							
							
							
							
