长兄俞启孝出生于北京，留学美国，回国后在天津当教授；老二俞启信在德国出生，专攻化学，在一家兵工厂工作，后来长期患病；老三即俞启威，生于北京，幼年随母亲住在南京，人称“三少爷”；老四俞启忠，学农，五十年代从美国回国，在北京当教授；俞家的大姐俞珊，其实是老大，出生于日本，喜爱文艺，成为演员。父亲以为名门出“戏子”，很不光彩，一度要登报脱离父女关系。后来，俞珊主演《沙乐美》、《卡门》，上海各报登载她的剧照、报道，受到社会尊重，很多朋友在她父亲跟前称赞她的成功，父亲也就只得作罢。俞珊后来嫁给当时正热心于戏剧改革的戏剧家赵太侔。赵太侔，又名赵畸，山东益都人，是１９３０年代国立山东大学的校长。 <br />
<br />
小妹就是俞瑾，一生从医，洁身自好，既不过问政治，也不爱好文艺。据俞瑾回忆，她出生后，家里还很阔绰。那时，俞宅在上海哈同路（今铜仁路），父亲出入总坐小汽车，只消给霞飞汽车公司挂一个电话，轿车马上便会***而来。但在俞明震去世之后，俞大纯依然花天酒地，家道日渐衰落。到了１９３０年前后，俞大纯在担任交通部陇海铁路局局长时，得罪了山东新军阀刘峙，丢了官，躲到上海家中“赋闲”，家境一落千丈。 <br />
<br />
