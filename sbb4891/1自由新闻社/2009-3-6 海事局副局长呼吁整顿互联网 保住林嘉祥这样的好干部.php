													<h2>海事局副局长呼吁整顿互联网 保住林嘉祥这样的好干部</h2>
						
						
																											<div id="postmessage_45749" class="t_msgfont">政协委员为涉嫌猥亵女童书记喊冤:他是个好干部<br />
2009年03月06日02:20 <br />
<br />
来源：四川在线 <br />
<br />
<img src="http://www.moc.gov.cn/zhuzhan/gongzuohuiyi/quanguorenda_ZXHY/2008rendazhengxie_HY/guancheluoshi/200803/W020080320338818861146.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　新快报特派记者 陈志龙 尹辉 陈红艳 陈琦钿 张潇<br />
　　去年底，深圳市海事局党组书记林嘉祥涉嫌猥亵女童一事闹得沸沸扬扬，深圳警方调查后公布结果称林嘉祥涉嫌猥亵罪不成立，属酒后行为失当，林嘉祥随后被撤销党内外职务。<br />
　　对于这一“猥亵门”事件，昨天，全国政协委员、交通运输部海事局常务副局长刘功臣接受新快报记者采访时表示，林嘉祥不可能再复职，但他的工资待遇予以保留，“网络是可以杀人的，他（林嘉祥）就是个倒霉蛋，”刘功臣为林嘉祥叫屈说。 <br />
<br />
<br />
　　肯定不会再安排林嘉祥工作<br />
　　刘功臣表示，林嘉祥的事件发生后，交通运输部海事局系统全面整顿了作风、加强了行政作风建设。他说，林嘉祥一事完全不是网络上传的那样，“公安（调查）不是有结果了吗？没有什么事了，但因为酒后失态就把他（林嘉祥）撤职了。我觉得现在的公务员是弱势群体，他就是喝多了，和别人吵架了，吵得厉害了。他是个倒霉蛋。”<br />
　　刘功臣说，林嘉祥“是一个多年的干部、很好的干部，谁不会有喝醉酒的时候呢”。对于林嘉祥是否被开除党籍一事，刘功臣澄清道：“凭什么开除他？他做什么了？处理得有依据呀。公安有认定吗？没有呀。他触犯法律了吗？难道还能判他刑啊？”<br />
　　既然没有大的过错，林嘉祥会否复职呢？刘功臣表示，不可能再给林嘉祥安排工作了，但工资待遇还是会保留。<br />
　　“他家里困难常喝闷酒”<br />
　　刘功臣认为，林嘉祥遭遇了网络暴力。“其实他（林嘉祥）家里比较困难，他老婆有重病，就一个女儿，身体状况也不好。他工作能力非常强，但经常去喝闷酒解愁，为这事我也批评过他几次，叫他喝酒不要过度。他一次去那个地方（出事酒店）吃饭，地方的人（当事女孩的父亲）是会员，非常富。”<br />
　　对于林嘉祥之前被报道涉嫌猥亵，刘功臣为他叫屈道：“他是老同志了，明年就要退休了，怎么可能做那种事呢？他喝醉了找厕所，叫小姑娘带一下路，问了一下&quot;你在哪上学&quot;之类的。”刘功臣说，事后林嘉祥告诉他，“问的时候拍了小姑娘一下，喝酒了手重嘛，小姑娘一看，这人喝醉了，扭头就跑了。”<br />
　　“整顿网络力度远远不够”<br />
　　至于网上流传的林嘉祥放言称自己是“交通部派来的，和你们市长级别一样高”，刘功臣表示，网上的视频打出了这些字幕，但摄像头画面是不会有字幕的，“他傻吗？当时现场有很多人，后来公安局调查了156个目击者，没一个人说林嘉祥说过那样的话。但就因为他在公共场合失态，影响一个领导干部的形象，就撤职了。”<br />
　　刘功臣认为，林嘉祥事件是受部分“仇官仇政府”的网民煽动。他表示，网络有操纵民意的作用，但重要的是，网络不能作假。“网络是可以杀人的，可以把黑的说成白的，把白的说成黑的。”因此，他认为目前国内整顿网络的力度远远不够。<br />
　　事件回顾<br />
　　据媒体报道，2008年10月29日晚，在深圳某酒店，一名中年男子涉嫌将11岁女童拖进洗手间内猥亵未遂。当女孩父母回头找该男子讲理时，男子竟叫嚣：“我是交通部派来的，级别和你们市长一样!”视频曝光后，经网民人肉搜索证实，这名男子是深圳海事局党组书记、副局长林嘉祥。<br />
　　10月31日下午，深圳海事局就此事召开新闻发布会，交通运输部海事局副局长瞿久刚宣布对林嘉祥进行停职调查，并将根据调查结果进行严肃处理。<br />
　　11月5日，深圳市公安局举行新闻发布会称，经调查认定，林嘉祥涉嫌猥亵罪不成立，仅属酒后行为失当，按治安案件处理。此后，交通运输部党组依据调查结论，决定给予林嘉祥撤销党内外职务的处分。</div>

							
							
							
							
