													<h2>开骂的来咯~~~</h2>
						
						
																											<div id="postmessage_271803" class="t_msgfont"><strong>破坏广场气势与辽阔视野巨柱永留天安门被轰灾难</strong><br />
<br />
生果日报网上版 09.10.6<br />
--------------------------------------------------------------------------------<br />
&nbsp;&nbsp;<br />
自发生西藏和新疆事件后，中共建政 60周年庆典为彰显 56个民族大团结，在天安门广场竖起 56根巨型民族团结柱。庆典过后，有报道指当局打算将它们永留广场，引起内地民众和专家的巨大反弹，批评巨柱与广场不协调，长留广场将是个灾难，破坏广场辽阔的视野。北京著名环保人士华新民更指，巨柱永留广场，后果比江泽民下令建造国家大剧院，破坏了长安街景观更加严重。<br />
<br />
《北京晨报》昨引述北京庆典群众游行指挥部艺术总监陈蔚指，为庆典临时竖立的 56根民族团结柱，很可能永久矗立在天安门广场；这将是继 1977年毛主席纪念堂落成后，天安门广场又一个新的建筑。报道称，巨柱突破了以往天安门广场庆典活动的平面布局，填补了人民大会堂与中国革命历史博物馆之间的间隔。但有关报道旋即引起内地舆论和民众的强烈反应。昨日，官方新华网发展论坛及新浪、搜狐和网易等门户网站的新闻跟贴，一反常态，出现大量质疑和批评的声音。网民指，这些巨柱与天安门周围景观极不谐调，严重破坏了广场的视野；且从艺术角度看，巨柱的艺术水平一般，毫无特色可言，决不能长留在广场。<br />
<br />
<strong>被讥「民族阳具」无助促进团结</strong>&nbsp;&nbsp;<br />
<br />
有网民指，中国近几年民族问题不断，西藏和新疆相继发生骚乱，「以为在天安门广场竖 56根民族团结柱，就可以促进民族团结，那是笑话！」「中华民族本来就是一个大家庭， 56根民族柱竖在那里，不是时刻提醒人们彼此是分开的吗？」「请神容易送神难，以后要拆，或者被破坏，就成了破坏民族团结了，后患无穷！」有网民更以「民族阳具」形容巨柱。本港美术家、前全国政协委员刘宇一昨日对本报表示，民族团结柱在庆典时增加气氛是可以的，「但如果永久放在天安门广场，绝对不合适」。他认为，天安门广场宽阔大气，四周的建筑也很讲究和谐，那些巨柱「不符合广场的总体构思」。北京现代城市形象研究所所长易居也指，将巨柱长置天安门广场「一定要慎重」，搞不好对广场的形象「将是一场灾难」。北京著名环保人士华新民女士指，过去几十年，天安门广场不断遭蚕食，面积越来越小，「现在的官员对城市管理和建设，没有任何值得称道的地方，也没有任何文物意识和环保概念。」她认为，那些巨柱如果永留在天安门广场，会比当年江泽民硬要在人民大会堂旁、长安街边建国家大剧院破坏了长安街的协调更加严重。 <br />
<br />
<strong>「把广场填满 不让人来反对」</strong>&nbsp;&nbsp;<br />
<br />
北京维权人士李健也指，天安门广场除了宽广大气，它还很有历史感，如果为了某些政治理念，在广场强塞进那几十根柱子，不但占据广场空间，也会令广场变得不伦不类，「我看当局是恨不得用甚么东西，把天安门广场都填满，不想让人民到广场来表达反对声音吧！」本报记者 <br />
