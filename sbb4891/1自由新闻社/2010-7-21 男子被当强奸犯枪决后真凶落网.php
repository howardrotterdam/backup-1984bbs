																											<div id="postmessage_751624" class="t_msgfont">男子被当强奸犯枪决后真凶落网 媒体揭洗冤实情<br />
 来源：大众网 2010年07月21日<br />
　1984年，由于当事人的误认和办案人员的不负责任，年仅23岁的河南小伙子魏清安被误判为强奸犯。5月3日，被执行枪决。随后的6月，真凶落网，并主动交待了犯罪经过。这惊动了有关部门。随后，河南省高级人民法院、省公安厅派人参加的联合调查组，对魏清安一案进行复查，如今回顾26年前的洗冤实录，仍让人心情复杂……<br />
<br />
　　1983年1月25日下午，河南省巩义市回郭镇干沟村村民刘某被强奸；<br />
<br />
　　1983年3月5日，警方安排被害人辨认作案者，魏清安被误认；<br />
<br />
　　1984年5月3日，魏清安被核准死刑，当日被执行枪决；<br />
<br />
　　1984年6月，真凶田玉修落网，主动供述了强奸刘某的犯罪事实。随后，当地公、检、法部门成立调查组进行了历时八个月的复查；<br />
<br />
　　1986年3月26日，河南省检察院向最高人民检察院提交关于魏清安一案的复查报告。最高人民检察院随即开始调卷审查；<br />
<br />
　　1986年七八月份，中央政法委和“两高”派出工作人员，组成联合调查组，赴河南对魏清安案件进行最后复查；<br />
<br />
　　1987年1月2日，“两高”联合发出关于魏清安案件的批复，认为原以强奸、抢劫罪判处魏清安死刑，实属冤杀，应予平反。<br />
<br />
　　法治周末记者 郭国松<br />
<br />
　　正是初夏时节，《法治周末》记者从郑州驱车一路向西，寻找一个26年前因“强奸罪”而被处决的人―――魏清安。<br />
<br />
　　除了魏清安这个早已不在人世的名字，我们并不知道他的家人。<br />
<br />
　　汽车来到魏清安生前居住的河南省巩义市回郭镇干沟村村口时，我们就开始打听：“知道一个叫魏清安的人吗？”“知道，被杀20多年了。”住在马路边的一户人家，两个看起来60多岁的老人用手指着斜对面的一栋房子，“魏清安的父亲就住在那个房子里”。<br />
<br />
　　如同每个农村家庭一样，魏家居住在一个面积不大的小院子里，一栋朝南的平顶房子稍大，紧挨着朝西方向的房子略小，显得非常破旧。<br />
<br />
　　魏清安的父亲魏有捞迎了出来。老人个子不高，虽已75岁，但身体很硬朗。<br />
<br />
　　在杂乱无章的客厅里坐下，当地朋友用河南话介绍说：“这是外地来的记者，想了解魏清安的案件。”“都20多年了……”老人刚一开口，便哽咽得说不出话来，坐在一旁的老伴也跟着伤心落泪。<br />
<br />
　　一晃过去了将近30年，第一次有记者前来调查魏清安冤案，我们的提问尽管小心翼翼，却不可避免地勾起了老人无限的伤感。<br />
<br />
　　刑场上大声喊冤<br />
<br />
　　1983年1月25日，正值春节前夕，中原大地寒气袭人。<br />
<br />
　　那天下午，邻居魏天续(音)家正在盖房子，从本村请了很多人帮忙，魏清安也在其中，他与几个年轻人轮班拉土。<br />
<br />
　　由于妻子临盆在即，魏清安不放心，趁拉土换班的间隙，不断地来回跑。下午5点，魏清安又一次回到家，他看了看妻子，便躺在床上睡着了。大约20分钟左右，村里兽医魏玉民来家里给猪打针，魏有捞叫儿子起来帮忙。魏玉民打完针离开时，太阳已经下山。<br />
<br />
　　就在下午5点左右，与干沟村相邻的西南岭村发生了一起拦路强奸、抢劫案，女青年刘某被一名20多岁的陌生男子强暴，并被抢走了手表和手提包。<br />
<br />
　　案件很快在附近村里传开了，人们议论纷纷，作出各种猜测。<br />
<br />
　　案发一个多月后的3月5日，干沟村的所有年轻人被通知去开会。魏有捞说，当时村里干部只是说年轻人都去开会，具体开什么会不知道。事后大家才明白，原来所谓的开会，其实是警方为了让被害人辨认作案者而故意安排的。<br />
<br />
　　魏清安也被通知去“开会”。他就这样离开了自己的家，留下一个刚满月的女儿，再也没有回来―――被害人刘某看了一眼魏清安，对现场的警察说：“看长相像是他，个子好像低了点。”<br />
<br />
　　审讯时，魏清安不止一次地申辩说：“肯定认错人了，我脸上有一块疤，还有一个红痣，案发那天我的左手受伤了，包着纱布，当时喉咙疼得厉害，说话的声音不一样……”<br />
<br />
　　魏清安的所有辩解都是徒劳的。至于他是在什么情况下承认强奸、抢劫刘某的，至今仍不为人知。唯一的线索是，魏清安被处决后，家人从看守所取回他盖的被子，拆洗时，发现被子里面缝了一个魏清安手写的纸条：“爸、妈，我对不起你们，我没有作案，他们非让我交代，打得受不了，我只好按照他们的要求说……”<br />
<br />
　　魏有捞还记得，当时有好几个警察到家里搜查，最后搜到一个提包，说作案时抢走的就是这个包。“我跟他们说，这是我开会发的奖品，上面印的还有字，怎么成了抢来的包呢”？<br />
<br />
　　魏清安被抓走后，魏有捞去找县公安局的办案人员。“我跟他们说，绝对不可能是清安做的，他当天一直都在给邻居盖房子拉土，很多人都可以证明，他根本没有时间去作案！”<br />
<br />
　　案件到了法院，魏有捞从邻近的偃师县请了律师，但法院不让参加辩护。魏有捞没办法见到身陷囹圄的儿子，只好反复向审判长交涉：“这可是人命关天哪，不能随便判，能不能判个死缓，人不杀，先挂着，如果确实是他做的，我连尸体都不收，俺一家人丢不起这个脸。”<br />
<br />
　　魏清安到底是怎么判决的，家人不仅没有得到任何消息，而且从未看到判决书。<br />
<br />
　　《法治周末》记者经过调查获悉，魏清安强奸、抢劫一案，由河南省巩县(现为巩义市)检察院提起公诉，巩县法院以强奸、抢劫罪，一审判处魏清安死刑，剥夺政治权利终身，郑州市中级人民法院二审维持原判。1984年5月3日，经河南省高级人民法院核准死刑，魏清安当日被执行枪决。<br />
<br />
　　据有关人士证实，判决书下达后，魏清安拒绝签字，要求法院出示他作案时穿的衣服、鞋子等证据。在当天行刑时，魏清安大声喊冤：“这个案子天长地久，总有一天会弄清楚的……”<br />
<br />
　　魏清安在刑场喊冤后，现场负责人下令暂停执行，紧急请示河南省高级人民法院分管领导。这位领导提出，从被害人阴道内提取的分泌物，经化验与魏清安血型一致，说明判决不可能错误。<br />
<br />
　　最终，时年23岁的魏清安被处决。魏有捞被通知去收尸时，才知道儿子已经被处决了。<br />
<br />
　　真凶在一个多月后落网<br />
<br />
　　魏清安临刑前的预言很快变成了现实―――1984年6月，洛阳市公安局抓获了一名叫田玉修的盗窃犯(当时的法律尚无嫌疑人称谓)，在被判处死刑后，河南省高级人民法院对其进行死刑复核，眼看就要拉去刑场了，田玉修向洛阳市公安局主动供述了他强奸、抢劫刘某的犯罪事实。<br />
<br />
　　当洛阳市公安局向案发地巩县公安机关查证时，两地警方不禁大吃一惊―――强奸、抢劫刘某的罪犯魏清安已经在半年前被处决，现在突然冒出一个田玉修，难道魏清安被冤杀？<br />
<br />
　　案件迅速上报到河南省公、检、法高层，省委有关领导获悉此事后感到极度震惊，省委政法委决定成立由省检察院牵头，省高级法院、省公安厅派人参加的联合调查组，对魏清安一案进行复查。<br />
<br />
　　现已退休的王绳祖首先接受了《法治周末》的采访。当年，他是河南省检察院刑事检察处处长，案件的复查工作基本上由他负责。除此外，当时的调查组成员还包括：时任省检察院检察员王连珠、省高级人民法院刑一庭副庭长刘怀珍、省公安厅处长葛中玉等。<br />
<br />
　　对于调查组来说，深入此案的第一道关口是对魏清安和田玉修两人口供的甄别。在几乎所有的关于作案过程的审讯笔录中，魏清安的供述很详细，甚至连细节的描述都与案发现场完全吻合；而田玉修的供述却显得很粗，有些地方与受害人的说法不一致。<br />
<br />
　　到底相信谁的供述呢？在一个口供还是主要证据，且口供处于案件的核心地位的刑事司法年代，要想排除口供是非常困难的。<br />
<br />
　　河南省高院有关领导据此提出质疑，如果不是魏清安作案，为什么他交代的作案情节能够如此细致，与现场完全吻合？如果是田玉修作案，为什么他交代的作案过程含糊不清，不少细节与现场存在出入？<br />
<br />
　　这当然是一个问题。省检察院主要领导和参与调查的王绳祖、王连珠两位检察官在事前阅卷时已经发现，魏清安的供述表面上看起来很详细，与受害人的证言和现场勘查高度一致，但魏清安的供述极不稳定，在11次审讯中，五次作了有罪供述，另外六次又推翻了供述。其中，有三次笔录记载，魏清安从审讯一开始就拒不承认，最后两次则完全不承认作案，要求公安机关拿出他犯罪的证据。<br />
<br />
　　作为检察官，他们从内心里隐约作出判断，这可能是案件侦查机关逼供、诱供、指供的结果。但在没有任何证据支持的情况下，这种主观推测只会引起更加尖锐的矛盾。所以，他们必须寻找证据。<br />
<br />
　　从关键物证上打开突破口<br />
<br />
　　案件发生在傍晚时分。受害人刘某正骑着自行车往家走，冷不防被一个同样骑自行车的陌生男子撞倒在地，自行车受损，两人争执起来。男子表示帮她修理自行车。恰在此时，过来一位外村人问路。待那人走远后，男子凶相毕露，将刘某拖至附近一间废弃的水泵房强奸，然后抢走了她的手表和手提包等物，骑上自行车逃之夭夭。<br />
<br />
　　时任河南省检察院检察长赵文隆与王绳祖等人一起，先后两次来到案发地点实地察看，发现水泵房位于野外，是一间没有门窗、四面透风的破房子。赵文隆提出，重点查找罪犯作案时所骑的自行车、所穿的大衣、皮鞋的下落、被抢手表的去向、魏清安有没有作案时间等事实。同时要查清魏清安是在什么样的情况和环境下供述的，是否存在刑讯逼供。<br />
<br />
　　调查组发现，在自行车、大衣、皮鞋、手表的来历和去向上，魏、田两人的供述差异较大。于是，调查组决定从本案的几个关键物证上打开突破口，对魏清案和田玉修分别供述的物证下落逐一追查，以判断两人供述的真假。<br />
<br />
　　第一个物证：自行车。魏清安供述说，他当天作案时所骑的自行车是从邻居李秀荣家借来的，后来又说是从另一个邻居魏田胥那里借来的，是魏田胥的亲戚放在他家的自行车。而自行车到底是什么牌子，魏清安表示记不得了。<br />
<br />
　　调查组找到了李秀荣和魏田胥，两人均证实，魏清安没有向他们借过自行车，以前的办案人员未向他们了解这一细节。<br />
<br />
　　调查组提审田玉修，他供述说，作案那天骑的是一辆“双喜牌”28型自行车，该车是他作案前不久从洛阳市郊区铁路护路队偷来的。当天强奸、抢劫刘某后，他骑着这辆车逃到附近的偃师县城，顺手丢弃在一个理发店的外墙边。<br />
<br />
　　理发店的员工向调查组证实，当时确实有一辆被丢弃的自行车，被居委会的工作人员送到了公安局。调查组带着田玉修，在公安局院内的一大堆自行车中辨认，他当即认出了作案时使用的那辆“双喜牌”28型自行车。<br />
<br />
　　为了进一步确认田玉修口供的真实性，调查组将当初现场勘验提取的自行车车胎痕迹证据与该车花纹比较，二者完全一致，证明案发现场遗留的自行车痕迹就是田玉修所骑的这辆自行车。而魏清安关于此车的来历等口供系编造。<br />
<br />
　　第二个物证：手表。魏清案供述：他将刘某强奸后，又抢走了她佩戴的手表，然后将其卖到洛阳，至于卖给了什么人，已经记不清楚。<br />
<br />
　　田玉修的供述则非常详细。在实施强奸行为后，田玉修将刘某的手表抢走，一直戴在手上。田玉修乃吃喝嫖赌、杀人越货之徒，罪恶累累，他一路流窜到郑州，又去嫖娼，最后却没钱“埋单”，被卖淫女扭送到郑州市大同路治保会，那块抢来的手表被治保会没收。<br />
<br />
　　调查组循线找到治保会工作人员，追查手表的下落，从治保会一路追到派出所，再到辖区公安分局，发现这里有很多收缴来的赃物手表。调查组带着受害人前往辨认。<br />
<br />
　　受害人刘某说，手表是几年前姐姐结婚时姐夫送给她的礼物，“广州”牌，曾到镇上一家修表店维修过。面对公安分局库房内历次办案收来的各种牌子的手表，刘某认出了她的“广州”牌手表。<br />
<br />
　　带着手表，调查组在刘某的引导下，找到了那家手表维修店。修表师傅拿过手表说，他有个习惯，凡是他维修过的手表，都要在手表后盖内部做一个三角形记号。师傅将手表后盖打开后，发现果然有个三角形记号。<br />
<br />
　　上述情节表明，魏清安供述查无实据，而田玉修所言完全真实。<br />
<br />
　　第三个物证：大衣。受害人刘某证实，当时强奸她的男子身穿大衣。魏清安的口供称，那天他去自家田里浇水，由于天气很冷，他从邻居魏镇豹那里借了一件大衣，作案时穿的就是那件大衣。<br />
<br />
　　调查组来到魏镇豹家里，证实这是一个虚假的供述，因为魏镇豹根本没有借给魏清安大衣，况且案发那天魏清安在帮邻居拉土盖房子，怎么会去自家田里浇水呢―――这个细节表明，原办案人员是多么的轻率，否则，稍加核实就能发现问题。<br />
<br />
　　对于大衣的来历，田玉修的供述就像讲故事一样生动―――田本是洛阳偃师县人，平时住在巩县回郭镇柏玉村，与魏清安所在的干沟村相邻。有一天，他路过附近的一个村，两个农民正在打架，其中的许某将大衣脱下来，让田玉修给他拿着。就在两个农民打得不可开交时，品行卑劣的田玉修趁机带着大衣跑了。在郑州嫖娼时，大衣和手表一样，都被大同路派出所暂扣。<br />
<br />
　　好在时间不久，调查组让大衣的原主人许某从大同路派出所收缴的一批大衣中辨认，他很容易找到了自己的大衣；再让田玉修辨认，他确认这件大衣就是当时作案时所穿的大衣。<br />
<br />
　　第四个物证：皮鞋。由于案发现场是庄稼地，遗留的足迹表明，作案者脚穿皮鞋，且鞋底钉有铁掌。魏清安供称，他作案时穿的是一双三接头的皮鞋，鞋底钉了四个铁掌。同时，魏清安还画了一副鞋底花纹图，与案发现场提取的足迹花纹一模一样。<br />
<br />
　　但是，经过认真调查，魏清安家从来没有三接头皮鞋，更没有在鞋底钉过铁掌。既然如此，魏清安为什么能够熟练地画出与现场完全一致的鞋底花纹呢？检察长赵文隆在一次有“两高”人员参加的讨论会上说：“这实际上是逼、诱、骗的结果，根本不可信。据我们调查，魏清安被关押期间，办案人员在审讯过程中，对他进行罚跪、捆打、电警棍捅、车轮战、不让他睡觉等手段。更令人气愤的是，趁家人给魏清安送饭之际，在馒头内夹纸条，以魏清安爱人的口气说：‘局长叫你承认，你就按他们的意思说，承认就没事了’。”<br />
<br />
　　“至于作案现场鞋底花纹的痕迹，巩县公安局派了一个‘耳目’叫杨某，为获取证据，公安民警刘某将皮鞋痕画好后交杨某诱使魏清安照画，甚至在审讯时魏清安画不圆，还是刘某提笔代画的，这就是问题的真相。”赵文隆说，“如果没有事前准备，在座的各位谁能准确地画出自己的鞋痕花纹呢？因此说，画得越准确，可信程度就越低。”<br />
<br />
　　再看田玉修的交代。他在作案后，将当天穿的皮鞋与他哥哥的作了对换。调查组几经查找，终于在山西的一个煤矿找到了田玉修的哥哥，证实田玉修交代属实，兄弟俩人对皮鞋外观的描述与现场相吻合。<br />
<br />
　　至此，魏清安被冤杀的事实已经毋庸置疑，剩下的只是通过司法程序予以纠正、有关部门进行善后赔偿的问题。<br />
<br />
　　案件复查遭遇重重阻力<br />
<br />
　　调查组历时八个月，基本查清了整个案件，确认魏清安强奸、抢劫一案系冤案，应当通过司法程序予以纠正。<br />
<br />
　　王绳祖说，当初立案调查时，虽然从上到下都表示支持，但后来的事实证明，那些所谓“依法办事，严肃查处”之类的表态，更多的是一种官腔。在案件调查过程中，阻力重重，特别是魏清安一案的原办案机关，不仅不配合，反而有意刁难，为案件的复查制造了许多障碍。<br />
<br />
　　以时任刑检处处长王绳祖为主的调查组起草了关于魏清安一案的复查报告，在省、市、县三级政法委和公、检、法部门有关人员参加的案情通报会上，原办案机关首先发难，纠缠一些枝节问题，试图否定调查结论。<br />
<br />
　　王绳祖怒不可遏，要求与魏清安案件有利害关系的单位予以回避，“既然是省检察院牵头，那我们就要一查到底！”<br />
<br />
　　无可奈何之下，时任检察院检察长的赵文隆只好进京向最高人民检察院请求协助，获得支持。1986年3月26日，河南省检察院向最高人民检察院提交了关于魏清安一案的复查报告。随后，最高人民检察院开始调卷审查，并会同中央政法委和最高人民法院研究此案的问题。<br />
<br />
　　大约在1986年七八月份，中央政法委和“两高”派出三名工作人员，组成联合调查组，赴河南对魏清安案件进行最后复查。<br />
<br />
　　调查组首先听取了赵文隆等人的汇报，对其中存在争议的问题进行了公开辩论。最高人民检察院刑事检察厅的陈燕麟提出，魏清安当初是由受害人秘密辨认出来的，为什么会发生错误呢？<br />
<br />
　　赵文隆说，根据刑事诉讼法的规定，辨认不能单独作为证据使用，而只是公安机关的一种侦查手段。即便如此，本案中的辨认环节也显得太草率。案件发生于1983年1月25日，辨认是在3月5日，相隔40来天，辨认的准确性值得怀疑；魏清安脸上有个疤痕，还长着一个红色的痣，当时左手受伤，被纱布包着。这些非常明显的外貌特征，受害人从未提到，这是不正常的。<br />
<br />
　　判决书认定，刘某被强奸的时间发生于1月25日下午5点5分。此时，一整天在为邻居魏天续拉土盖房的魏清安刚回家，躺下休息一会，正巧村里兽医魏玉民来家里给猪打针，他与父亲一起，给魏玉民帮忙。魏玉民提供的处方上记载的时间是下午5点21分。从魏清安家到案发地相距4公里，步行约需一个小时，骑自行车大约25分钟。无论采取哪一种方式，魏清安都没有作案时间。这些事实有很多人可以作证，但原办案人员既没有实地调查，也不听魏清安的辩解，凭主观臆断，草草定案。<br />
<br />
　　而另一个在理论上很可靠的技术性问题，却出现了低级错误―――刘某报案后，法医提取了精斑，经检验，精斑为“O”型分泌型，与魏清安血型一致。由于这项技术鉴定的支持，办案人员确认魏清安就是作案者。河南省高级法院在进行死刑复核时，除了魏清安的口供外，认为本案最为核心的证据，一是受害人的辨认，二是精斑的血型化验，是刑事科学鉴定，既不会冤枉好人，也不会放过坏人。在魏清安临刑前喊冤时，执行人员紧急报告省高院分管领导，这位领导就是以“这个鉴定不会错”为理由，最终下令执行。<br />
<br />
　　但是，从侦查到判决，显然忽视了一个常识性问题。因为血型鉴定不同于DNA，后者可以达到99.99%以上的准确率，其鉴定结果具有唯一性和排他性；但血型鉴定不同，因为人的血型只有A型、B型、AB型、O型以及部分特殊类型，每一种血型都有巨大的群体。<br />
<br />
　　魏清安一案的办案人员和审判机关的某些人开始还在狡辩，认为血型鉴定证明作案者就是魏清安。直到抓获田玉修，田的血型也是O型，使得一部分人在证据面前，终于意识到此前对魏清安一案血型鉴定的常识性错误。<br />
<br />
　　终于获得平反<br />
<br />
　　在重重阻力下，魏清安冤案的再审程序拖延了将近三年。1987年1月2日，最高人民检察院、最高人民法院联合发出关于魏清安案件的批复：你们汇报请示的魏清安一案，经我们共同阅卷和核查，认为原以强奸、抢劫罪判处魏清安死刑，实属冤杀，应予平反，并做好善后工作。<br />
<br />
　　据此，河南省检察院提出抗诉，认为本案事实不清，证据不足，请求河南省高级人民法院宣告魏清安无罪。河南省高级人民法院依照审判监督程序作出再审判决，撤销巩县法院(83)刑一字第98号判决和郑州市中级法院(83)刑二字第177号判决，宣告魏清安无罪。<br />
<br />
　　有关部门就善后赔偿等问题征求魏清安父亲魏有捞的意见时，他没有提出任何过分的要求。“我当年就跟他们说，你们看着办吧，孩子也没有了，咱总不能拿孩子换钱。”魏有捞提出了30000元赔偿要求，最后给了他33000元。同时，有关部门给魏清安的妻子、女儿以及他的一个弟弟共3人解决了农转非户口。<br />
<br />
　　善后工作结束后，时任河南省检察院检察长赵文隆主持本院、郑州市、巩县三级检察院负责人会议，总结魏清安冤案的教训，决定对造成此案的相关责任人立案查处。<br />
<br />
　　来源：法治周末</div>

							
							
							
							
