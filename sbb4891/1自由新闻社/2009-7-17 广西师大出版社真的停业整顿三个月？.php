																											<div id="postmessage_166178" class="t_msgfont">《大学人文读本》总序<br />
⊙夏中义<br />
<br />
??当我为即将付梓的《大学人文读本》（以下简称《读本》，广西师范大学出版社版）伏案作序时，我明白，我所想写的，不仅仅代表我个人。<br />
??有人问：你们为何要编这套《读本》？<br />
??这可从两方面来回答。若着眼于建设性，《读本》旨在为当下中国大学生的“精神成人”提供系统的优质思想资源；若着眼于补偿性，则无须讳言，中国高校人文教育缺失久矣。<br />
??大学人文教育之长期缺失，实为结构性缺失。且不说现实社会对大学的期待，一般重在专业乃至功利，而非人文关怀；也不说大学生在校往往囿于学科分支细化而诱发人文视野萎缩，以致“学哲学太空，学历史太死，学文学太浅”，本属人文学科的文、史、哲尚且如此，再遑论非人文学科的理、工、医、农等专业了；即使是高校专职做学生工作的，也因偏于政治导向与心理调节，而无力亦无计从“精神成人”的高度来满足大学生对普世价值通识的人文渴求――虽然一个大学生从18岁跻身高等学府，到22岁本科毕业，本是其生命史特有的“灵魂发育”季节，他本就有权要求大学设置有关“精神营养”教程；虽然一个现代意义的大学，当它刚从中世纪的欧洲大陆崛起时，它便是以“精神城堡”的英姿，而非“职业培训所”的招牌，昭示于人类社会的。应该说，20世纪上半叶中国曾经有过这样的大学，如“五四”新文化运动时的北京大学，又如抗日战争时期的西南联大……然俱往矣，今非昔比，但无碍后人来日可追先哲。<br />
??《读本》应是一套普适性的青年知识分子修养读物，而不仅仅是大学人文系科的教参资料。这里所谓的“知识分子”，主要是指专业学识层面上的创造者、传播者及运用者，它可以涵盖学界、思想界那些敢于担当人间道义的“精神战士”，但前者的外延显然比后者宽泛。至于“精神成人”，则是强调一个普通的大学生应在本科期间初具“独立精神、自由思想”之潜质。其鉴别尺度之一，当是看其在学业之余，能否认真且持续地向自己追问“如何做人”这一终极命题，以及在何种价值水平上思索乃至践履此命题。而《读本》的创意正在于，欲引导读者从如下三个维度，展开对上述命题的思考：<br />
??第一， 人与自我――你将如何为自身的日常生存注入意义，从而使生物学层面的个体生命真正转化为文化学层面的“主体角色”；<br />
??第二，人与国家――你将如何面对故土的百年沧桑及其社会――文化转型，以期将自己塑造成迥异于卑微子民的“现代国民”；<br />
??第三，人与世界――你将如何置身于新世纪的“全球化”格局，尝试用全人类而非狭隘的眼光，来关注我们这颗星球所发生的宏大事件与国际难题，诸如生态、种族、战争、宗教、人权……而无愧为“世界公民”。<br />
??上述三个维度或三大理念，实已暗示整套《读本》的构成框架或编纂规则：以理念分卷，按主词立章。所谓“以理念分卷”，是指每一分卷重在阐释一个理念，依次为：“主体角色”（第一卷）；“现代国民”（第二卷）；“世界公民”（第三卷）。所谓“按主词立章”，是指围绕给定分卷之理念，设置一串富有逻辑关联的主（题）词（如第一卷为“主体角色”理念设置的“人与自我”主词系列是：“大学”“人生”“日常”“良心”“青春”“婚恋”“女性”“角色”“勇气”“遗嘱”），再依据每一主词内涵的深广延绵，来精心选文，有机组合，独立成章。简言之，若曰“理念”是贯穿“主词”系列的灵魂，则刻意编排的“主词”当是逻辑地支撑“理念”的骨架。<br />
??《读本》既然意在为大学生“精神成人”呈示根基性价值参照或“精神底线”，这便从根本上规定了《读本》内容，亦即选文须具备“思想性”、“可读性”与“经典性”。“思想性”、“可读性”无须赘言，“经典性”则重在辑录名家文字（内含著述、演讲、诗文、信函、奏章、碑铭、日记、随笔、格言、遗嘱诸多文体）。所谓名家，除了星辰般辉映20世纪人类文化星空的世界巨人外，《读本》还尤其珍视如下三类中华英杰所贡献的思想瑰宝：一是1840年以降为祖国的独立、富强与进步而鞠躬尽瘁的仁人志士；二是1916～1919年间为中国文化的现代转型而疾呼“民主与科学”的“五四”先驱；三是晚近20余年来为当代中国“改革开放”而不懈奋斗的社会精英。<br />
??这就意味着，《读本》所做的，实是在尝试现代人文视野的重新整合，即通过对人类共同的普世价值谱系的纵深勘探暨合理配方，以期为大学生“精神成人”提供全方位、几近全息型的思想营养。这就明显越出了当下高校系科设置互为阻隔的基本格局，而使《读本》初步呈现出跨学科、又渐趋圆融的大人文视野。是的，若着意甄别惯常意义上的学科归属，《读本》可谓涉猎甚广，近乎学科“博物馆”，远不止文、史、哲、经、政，还有伦理学、心理学、教育学、科学哲学、科学史、精神史……但有趣的是，隶属上述学科的选文一旦被有机辑入《读本》所设定的语境，它又奇迹般地淡出原先学科圈，而让文本的每一个字甚至标点皆无声地用来澄明大学生精神成人时可能生发的困惑。由于近代分工所铸成的高校系科的功能互阻，而今却在《读本》这一实验性思想平台消失于无形，这倒是我始料不及的。<br />
??诚然，《读本》不会忘记它的潜在读者首先是正在校园就读的大学生。大学生最终认同与否，可谓从根子上决定了《读本》的命运。为了让大学生在“灵魂发育”之际，能及时而确凿地听到另一种清醒而清洁的声音，《读本》颇讲究一个“贴”字。“贴”，要义有二：一是珍惜大学生内心的人文渴求；二是平等地正面应对大学生“成长的烦恼”――既不是居高临下地借权威语式来以偏概全，也不是把人文教育柔化为慈善家的安抚，更不是放任自流，眼看一群群大孩子因挡不住时尚的诱惑而及时行乐，以欲念的满足来遮蔽人生真谛，却一筹莫展。<br />
??让《读本》平等地正面应对大学生的人文渴求，这既是编委的集体行为取向，也是对某种阅读理想的预设。若读者日后真能将《读本》之阅读转化为“灵魂对话”，这当是编者所梦寐以求的。由此不难理解本书为何要将每章编者絮语命名为“旁白”。“旁白”者，既含“参与”之意，又有“代庖”之戒也。毕竟“灵魂”之事，纯属个人精神事件，他者可供参照，无法代之思考。<br />
??一部书稿，近百万字，好不容易编出来，末了却说编者对全书未必洞明若烛，相反，尚存疑点有待日后三思，这并非谦辞，倒是实话实说。<br />
??中国大学人文教育缺失既久，这便命定《读本》作为补偿性举措，近乎是在先哲曾经走过却又转眼荒芜的故土，重新踏出一条路来。我与我的编委一路走来，走了一年，似乎不曾有过“我们走在大路上”的豪迈。其间虽不缺“柳暗花明”之欣慰，那也是“山重水复”累月跋涉所致。是的，我不讳言《读本》是在尝试大人文视野之整合，但这片刚被整合的大人文视野对编者来说，无疑也很新，充满陌生化效果。我愿说我对全书构成早已廓然于胸，但我委实不敢说，我对《读本》每卷、每章乃至每篇选文对给定理念的细部阐释，也烂熟于心。我只是对与我专业相关的《读本》内容才有言说的自信。这就是说，面对《读本》所庄严展开的思想的宽银幕，我深感自己既是编者，同时也是亟须重新受洗的“教徒”，此“洗礼”所指亦有二：既指学识，更指灵魂。从这意义上，也可谓我和我的编委，是差不多与读者站在同一地平线上。<br />
??我们不是传教士，也不是舍身为人间盗火种的普罗米修斯，我们所以从五湖四海聚到独秀峰下，齐心协编《读本》，是因为我们皆祈愿能有这么一个机遇，可将曾真诚地感动过我们、并将继续深刻影响我们生命历程的那些思想暨文字，编成一本书，让现在那些渴望精神成人的大学生（其间将有我的孩子）也用心读一读，或许他们也会有所感动，有所思考。<br />
??最后，我要感谢广西师范大学出版社及其社长萧启明，责编赵明节、杨胜刚，感谢他们在《读本》一案所体现的出版家的眼光、气度与责任心。<br />
