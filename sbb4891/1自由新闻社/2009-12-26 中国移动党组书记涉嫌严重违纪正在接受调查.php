<br />
关于张春江的职务方向，电信运营商圈内早有各种传言。在2004年，中国网通在香港上市期间就有消息传张春江在IPO完成后将调往黑龙江省任副省长，但此后再无下文。 <br />
<br />
也曾有消息称，中国移动集团总裁王建宙已经59岁，离国资委规定的央企领导人60岁退休年龄只有半年时间了（王1948年12月出生），而张春江出生于1958年7月，显然是为接班而来，因为中移动此前并没有设副董事长一职。 <br />
<br />
今年2月，又有报道指出，张春江可能会离开中国移动到中国联通任职，但数月过去，未见动静。 <br />
