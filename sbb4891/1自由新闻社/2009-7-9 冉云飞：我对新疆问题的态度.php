													<h2>冉云飞：我对新疆问题的态度</h2>
						
						
																											<div id="postmessage_154520" class="t_msgfont">去年3月20日我写过一篇文章叫《我对西藏问题的态度》，在各大网站转载得不少，被删掉得就更多。我至今反观我那篇文章，我还是比较认可自己的观点的，甚至觉得有许多地方是可以移植过来作为我对新疆问题的态度的。新疆问题与西藏问题有一定的共通之处，但也有一定的差别，因此我把《我对西藏问题的态度》作为“延伸阅读”附在后面，以便大家批评指正。<br />
<br />
 <br />
<br />
在发表我的看法之前，我想说我说的不一定都对，甚或有相当之谬误，因为我也受消息封锁之限，所得不多，且是临时急就，卑之无甚高论，因此欢迎大家批评补充。但我希望大家尊重言论自由和理性思考的原则，我只是表达我的态度，除我之外，我并不代表任何其他人表态，因为我只是普通的一位公民。对一件公共事件表达我的态度，这是我的人权中不容剥夺的一项权利。不论附议还是反对我的表态，那都是你的权利。自我表态，这是证明自己还活着的一件证据，如此而已。<br />
<br />
 <br />
<br />
一：向所有死难者及其家属公开表达哀悼。我认为不妨在新疆乃至全国降半旗一天，以哀悼所有死难者。暴徒无疑应该谴责，无辜者的死伤令我们悲伤与愤怒。但从更深层的角度看，暴徒和无辜死难者都是受害者，都受专制制度、仇恨等方面的捆绑。<br />
<br />
 <br />
<br />
二：吁请官方解除信息封锁，使信息自由流通。封锁信息不会使沸腾的议题有所减少，只不过将议论屏蔽掉而达致掩耳盗铃。知情权越充足，越使人们在比较当中有自己的思考而趋于理性。在一个人们越来越认可多元信息、进行自我对批和判断的时代，进行信息封锁是愚蠢的，因为只有亏心的人，才害怕真相。<br />
<br />
 <br />
<br />
三：谴责任何暴力。暴力、复仇、冤冤相报，是通往地狱之门。即便你有幸活在这个世界上，如果能的暴力不除（正当防卫除外），你甚至滥施暴力，那就只好等着别人的反弹与复仇，这正是活在现实的地狱里。恐惧、防范、仇恨，使得活着也只不过是行尸走肉，并不是有质量的生活。我反对所有的暴力，批评个体的暴力，反对恐怖组织的暴力，也批评政府滥施暴力。<br />
<br />
 <br />
<br />
四：开通对话渠道，建设对话机制。谴责暴力，又要让大家的利益都得到相对公正的保护，那么在制度保障的情况下，随时敞开对话的大门。主政者尤其要接受来自民众（不管是汉还是维，还是其他少数民族）的询问、质疑、对话，以期在具体问题的解决机制中，重建互相的信任。<br />
<br />
 <br />
<br />
五：吁请更加务实而公正的民族政策。民族歧视，有看得见的，有看不见的，后者更为顽固。但应该消除那些可以通过制度和协调解决掉的现实歧视，如招工就业等方面的歧视。通过解决现实歧视，在制度保障下实现平等，那么心理意识上的歧视才会慢慢淡化乃至消弥。公正平等的民族政策不能停留在纸面上。<br />
<br />
 <br />
<br />
六：检讨诸种错误并道歉。四九年后的民族政策，既有承袭前苏联之弊，亦有自身的诸多问题，这一切都需要主政者公开的检讨与道歉。那种一出事就把责任推到境外敌对势力和少数暴徒身上的取巧做法，看似聪明，实在愚不可及。稍有判断人，都不会相信这种重复了几十年的老套说辞。退一万步说，六十年了，一直都有境外势力和少数暴徒，那么我们也应该想想他们存在的土壤，是不是有我们自身的问题？这自身的问题是不是应该检讨乃至道歉？<br />
<br />
 <br />
<br />
七：不能让无辜的底层民众为政府失策背书。这几十年来民族政策方面的失误，已经有很多人在探讨，网上可读的文章也有一些。但官方似乎不愿意放下架子来理智探讨这些问题，强硬、傲慢、呆板，已成为官方解决棘手问题的僵化思维，甚至成了他们的心魔。维汉冲突固然有历史原因，但更多是因为现实利益，无论如何，政府负有不可推卸的责任。<br />
<br />
 <br />
<br />
八：呼吁和解与宽恕。和解宽恕，当然应该建在真相与正义的基础上，没有这一切，和解宽恕就会变成无源之水，无根之木。所以我认为要速查并公布此次事件（包括韶关事件）真相，并让民众来批评、判断和考量。我们都应有一颗宽恕的心。在现实生存境遇中，和在通往天国的路上，我们都应有一颗宽恕的心，虽然这在一个不公平的环境里很难做到。但是我们每天不剔除仇恨之心一点点，不增加宽恕之心一点点，那么我们永远没有获得解放和自由之期。<br />
<br />
 <br />
<br />
 <br />
