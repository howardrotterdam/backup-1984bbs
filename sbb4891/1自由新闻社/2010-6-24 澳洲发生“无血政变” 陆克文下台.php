													<h2>矿山巨头因资源税赶走澳大利亚总理陆克文</h2>
						
						
																											<div id="postmessage_702595" class="t_msgfont">资源税赶走了陆克文 澳矿山巨头政治影响力浮出水面<br />
<br />
　　记者 杨烨 王婧 实习记者 张媛 北京报道<br />
<br />
　　因为盛产“疯狂的石头”而备受国内关注的澳大利亚，如今却上演了更加疯狂的一幕。24日7时，澳大利亚执政党工党举行党内选举，在获得“零支持”后，陆克文的总理职位被副总理朱莉娅・吉拉德取代，国库部长韦恩・斯旺将担任副总理。<br />
<br />
　　“这虽然是一个党内选举，却更多体现了政策和矿业巨头的博弈。”一位澳洲矿山资深人士在接受《经济参考报》记者采访时坦言，陆克文这个曾经被誉为澳大利亚最受欢迎的总理的下台，导火索就是前期一直争论不休的超额资源税。<br />
<br />
　　记者了解到，在前总理陆克文的推动下，5月份澳大利亚政府曾表示要向采矿等资源行业推行高达40%的超额利润税，表示这项税收将使“取之于民”的资源产品收入通过税收转移“用之于民”。但是如此高税马上引起了澳大利亚国内矿山的强烈反对，一些大的矿山如必和必拓和力拓公司都曾表示要暂缓矿山开发项目。澳矿业大亨帕默尔更是耸人听闻，声称这一税收将终结其蓬勃发展的历史。此外，大小矿商也纷纷抱怨政府仅提前三天通告就推行这样一个与行业利益紧密相关的税收制度，且不与行业广泛协商的做法不可接受，这令新税推行更加举步维艰。<br />
<br />
　　由税收引发了国内选民对后期经济和就业的担忧，各界都开始对陆克文的做法感到不满。记者了解到，自今年四月以来，陆克文的民调支持率节节下滑。“执政工党的党内洗牌，是为了减少陆克文支持率持续低迷对工党的影响，以谋求工党在今年晚些时候的议会选举中继续保持优势。”上述人士对记者说。<br />
<br />
　　相比较陆克文的性格，新上任的吉拉德用“微笑和专注”获得了更多的支持。据了解，此前民意调查均表明，吉拉德的受欢迎程度明显高于陆克文，甚至已有预测机构表示，工党将很可能赢得选举。与信奉天主教、不受女性欢迎的反对党领袖托尼・阿博特相比，吉拉德有望获得更广泛选民的支持。同时，头顶“首位女总理”头衔的她将在上任初期抓足媒体眼球。<br />
<br />
　　但是光靠微笑是远远不够的。吉拉德要做的更多的是扭转目前不利局面。实际上，吉拉德在当选当天就召开新闻发布会，号召结束政府与采矿业的紧张关系，推动政府和采矿业就资源税问题进行重新谈判。海外分析人士表示，从吉拉德的从政经历和风格看，可以肯定的是，工党未来处事方式将有所改变，尤其是针对此间各方聚焦的超额资源税。下一步，人们将看到政府与资源行业进行更多和充足的协商。<br />
<br />
　　实际上，除了澳大利亚本国企业外，上述政策也曾让中国企业神经紧绷。国内钢铁业相关人士在接受《经济参考报》记者采访时曾表示担忧，除了对中国企业在澳的矿产投资将受到很大打击外，对于拥有垄断话语权的“两拓”而言，必然将会通过抬高铁矿石价格，将税负压力转嫁到下游钢铁行业，作为最大的铁矿石进口国，国内钢厂将成为该政策最大的“买单者”。<br />
<br />
　　“但是从目前的情况分析，上述资源税的出台可能会有很大变化的空间。”我的钢铁网资讯总监徐向春在接受《经济参考报》记者采访时表示。如果资源税的取消成为现实，一方面对于在澳大利亚投资矿山的中国企业是件好事；另一方面，作为最大的铁矿石生产国，澳大利亚国内投资政策的稳定，也有利于铁矿石供应和价格的稳定。<br />
<br />
　　“但是中国企业更该感到的是担忧。”徐向春认为，从陆克文的下台不难看出澳大利亚矿山巨头对政治的影响力之大，这对于在澳大利亚投资的中国企业显然不是一个利好。另一方面，对于“两拓”在铁矿石全球策略，澳大利亚政府无疑将会起到推波助澜的作用，澳大利亚政府很有可能沦为三大矿山的呐喊者。综上所述，中国企业此时更应该考虑的是海外投资战略的多元化和对矿产资源安全性的危机意识的增强。<br />
