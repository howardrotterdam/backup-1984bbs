迁移到海外的“水木社区”纪念“水木清华”被内化五周年 （心语网站截图/RFA）<br />
<br />
“水木清华”事件5周年 大学言论封杀更甚<br />
<br />
当局封锁BBS网络“水木清华”5周年，网络审查愈加严峻，学生们表示，对网络封锁十分排斥，他们不会从校内网站获得资讯。<br />
<br />
据自由亚洲电台记者心语报导，星期二，中国大陆的网络上出现了一大批纪念文章，很多已经从大学毕业多年的人们从全球各地发出了悼念“水木清华”的声音。他们表示，事件已经发生了5年，现在网络审查范围更广，力度更大。 <br />
<br />
“水木清华”事件起源于2005年3月16日下午3时左右，清华大学网络中心对校内水木清华BBS全站实施了校外路由的屏蔽，使得校外用户无法访问。同时，进站画面声明：“从即日起，BBS水木清华站由开放型转为校内型，限制校外IP使用”。接着，校内几乎所有公开的BBS穿梭地址被断。此事件俗称“316事件”。之后网络中心教师陈茂科在学校的授意下，抢夺水木清华 BBS 服务器硬盘，造成水木清华站管理人员从清华出走，另开设水木社区站 (newsmth.net) 。同年，陈茂科却因此当选清华大学优秀共产党员。清华学生之后也为此写信给胡锦涛，却都无济于事。 <br />
<br />
一位经历了事件的北大毕业生阮吉告诉自由亚洲电台记者：“因为它虽然在清华，但实 际上用户是全国各地的，好多学生都喜欢到那里去，因为里面交流比较活跃，人也比较自由，它是一个非常好的交流平台，但是当时监管还是非常严的，比如在水木里面可以随意删你的帖子，有时候敏感的话题在时事讨论区里面不能提，删贴非常严重，但是即便如此，人们还是喜欢这个地方。你可以删除一些帖子，但是不可能把所有的帖子都删光。而且水木里面的管理基本上是学生，所以一般说是比较Tolerate（容忍）各种各样的思想。” <br />
<br />
阮吉认为，关闭水木清华BBS网站，目的就是为了控制言论：“教育部觉得学生思想太活跃，对他们可能统治不利，然后就把这些全部管到校内，校园外部的人就不能看到了，这样能够减少影响力。教育网2005年关闭这些校园访问就是一种类型的GFW，是教育网中的防火长城。” <br />
<br />
“水木清华”事件之后，中国大学的论坛大规模萎缩，都逐渐进入了更容易监管的校内登录模式，也接近于实名制。此外，在大学内部，也设立了各种言论控制体系，包括大学生的相互告密制度，包括学生之间和学生对教师的监督。2008年11月，在上海华东政法大学曾经出现杨师群老师讲解历史真相时被学生告密为反革命的事件，引起公众哗然。有评论认为，中国大学最核心的问题是没有学术和思想自由，官方完全用洗脑方式控制学生思维，设立教育网并长期严控就是最直接的手段。在近日校内网上流行却又屡遭删除的帖子“洗脑秘笈十八招三式”中，罗列了中国教育中惯用的洗脑手段，虽然人人都有感觉，但是却难以反抗。<br />
<br />
《洗脑秘笈十八招三式》<a href="https://1984bbs.com/viewthread.php?tid=40702" target="_blank">https://1984bbs.com/viewthread.php?tid=40702</a><br />
<br />
一位华东理工大学的在校学生一粟表示：“我们每个高校自己都建有BBS，至少都有一个，我们学校也出过事情，后来实名制以后就慢慢衰落下来。”<br />
<br />
网友何亮在标题为“又到316”的博客文章中表示：“5年来，网络上的信息封锁愈演愈烈。看到全球访问量最高的几个网站Facebook, Twitter, Youtube, Blogspot全部被挡在墙外，看到全球最大的搜索引擎扬言退出中国，看到一切在国内苟延残喘的Web2.0 站尽管积极自我阉割仍然朝不保夕，看到绿坝和孔子学院给大家开了几千万元的笑话，我真的不知道还有多少信心。”<br />
<br />
山东某大学一位不方便透露姓名被校方派去参与网络管理的大学生告诉记者：“包括网络域名劫持，还有GFW这些，我也有一部分涉及。因为我个人非常反感GFW，所以我尽量用自己的能力把这些因素尽量排除掉。” <br />
<br />
大学，原本是学术自由交流之处，校方鼓励学生们的自由创作；然而，在中国，自由上网获取信息都被严格限制，更不用提在课堂内自由阐述民主人权思想。<br />
