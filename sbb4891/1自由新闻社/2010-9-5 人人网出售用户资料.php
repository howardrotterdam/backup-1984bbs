我在写这篇文章之前，就知道这篇文章不会被任何一家国内主流媒体转载，强大的媒体公关已经变成互联网企业作恶的保护伞。<br />
<br />
一阁（@yegle）的Gmail收到了一封来自团购网站的宣传邮件，在这个团购网站泛滥的年代它们总是抓住各种方法来强奸用户的眼睛，有的不仅默认发送这些信息而且没有取消订阅的功能，这都不稀奇。<br />
<br />
但是这家团购网站似乎他没有什么印象，好像从来没有注册过，那么他们又是从哪里获取到自己资料的呢？这时候他注意到网站使用的会员ID是“请使用真实姓名”，这让他想起自己的人人网ID被封禁，之后被管理员修改名字为“请使用真实姓名”，真实的答案呼之欲出了。<br />
