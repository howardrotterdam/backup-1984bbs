冯正虎与王家瑞在日本入境口相遇<br />
舒文<br />
2009年11月8日下午约2：15，冯正虎闭着眼静坐在椅子上，一个中国政府代表团从他面前通过，相隔一步之差，这些高官们会清楚地看到，冯正虎右边椅子上披放着醒目的黑字白底的抗议文汗衫，左边的小旅行箱上套着一件写着一个很大“冤”字及“归国难”文字的背心。<br />
<br />
忽然，冯正虎睁开眼睛，一看已离他四五步的人群中，一个人很熟悉，冯正虎起身走上去，大喊一声“王家瑞”。王家瑞顿时站住，回头一看，他们之间仅隔二、三步，冯正虎同时指着椅子上的抗议文汗衫说：“你看，我被拒绝回国，住在这里，请你告诉大使馆。”王家瑞在几个同行人的簇拥下，几步就进入外交通道口。当时，冯正虎可以把他作为中国政府高官拦住诉苦，但冯正虎没有这样做，他不愿使这位同门师弟尴尬。<br />
<br />
正好，有一位随同人员被日本出入境审查官拦住，她持有的不是外交护照，不可以一起通过外交通道，要填写出境卡后从外国人通道走。填写出境卡的地方正是冯正虎的暂居地。这位随同人员约四、五十岁，是一位女性的。她也看到刚才冯正虎与王家瑞打招呼的情景，她问冯正虎；“你们认识？”冯正虎回答：“是的，我们是复旦大学的同学，一个导师。”“你看，我被上海当局八次禁止回国，困在这里。请你帮我转两份材料给他，好吗？”她说：“好的。”她继续在填入境卡，冯正虎取来三份材料，托她转交，她收下了。<br />
<br />
王家瑞是中国共产党对外联络部部长，与冯正虎师承同一个导师-----中国最著名的管理学家、复旦大学终身教授苏东水先生。冯正虎1983年考入复旦大学经济管理专业，是苏东水教授的第一批研究生，当时只有六个学生同班，苏教授的研究生仅是三人。王家瑞是90年代进入复旦大学，是苏东水教授的博士后研究生，当时他已是青岛市市长。冯正虎说：“我与王家瑞先生都与导师的关系很亲近，但由于我们身份的不同，彼此不联系，仅在导师组织的会议上见面，最后一次见面还是在导师的贺寿会上。王家瑞先生为人谦和、思想开明，是一个亲民的官员。”<br />
<br />
或许是上天的安排。冯正虎在日本国门外静坐抗议，呼吁中国政府关注中国公民的回国权，而首次出现在这个场合的中国政府高级官员竟然是他的同学。王家瑞先生是为中国国家副主席习近平访日做准备赴日的，或许他没有时间关心冯正虎的回国权问题，但是他及其率领的代表团成员都亲眼目睹这个场景：一个中国的高级知识分子被上海当局逼成国际访民，在每天入境日本的成千上万来自各国的旅客前展示自己的悲哀------一个中国公民无法回国，这也是中国的悲哀，这些侵犯中国公民基本人权的上海权势者最终使中国政府蒙上耻辱。<br />
<br />
冯正虎碰上王家瑞是必然的。所有入境日本的各国部长级人物都会看到冯正虎的静坐请愿，因为这是他们的必经通道，接着来日的中国政府高官也将会与冯正虎相见。当然国家元首级人物是乘专机访日，不会碰上冯正虎。冯正虎在日本国的大门外忍受羞辱与饥饿，是为了唤醒中国政府对人权的尊重，是希望每个中国人都有自由回国回家的权利。<br />
