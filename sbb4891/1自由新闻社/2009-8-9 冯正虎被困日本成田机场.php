																											<div id="postmessage_196908" class="t_msgfont">冯正虎第七次回国闯关的纪实<br />
&nbsp; &nbsp; <br />
2009年7月31日，我进行第七次回国闯关。前三次我的回国行动因遭受美国西北航空公司的阻扰，致使我无法抵达国内，这次决定改乘全日本航空公司的航班回国。当日下午4：00到达日本成田机场，准备乘晚上18：10起飞的NH0921航班，不知什么原因航班延迟起飞，晚上20：30左右抵达上海。我非常高兴，当晚又可以住宿在自己的国家里，感受祖国的气息，与不可一世的上海权势者直面交锋。谁阻扰我们回国回家，我们就向谁挑战，向谁讨回中国公民的回国权。<br />
<br />
一、&nbsp; &nbsp; &nbsp; &nbsp; 驱逐出境的中国公民<br />
<br />
飞机停靠后，机舱门一打开，前排的乘客们纷纷起身向前走动。我的座位42H，最后倒数第三排，离机舱门最远，排队最后。我边走边打手机，给兄长通报一声，向其他朋友问好，大家都不知道我今天回国。我清楚，机舱门口有警察等我，见到他们，我就无法再与外界联系了。我这次事前没有发公告，谁也不通知就突然回国了。或许，我是给国内当局一个面子，我的回国公告令他们尴尬丢丑。或许，我也不忍心上海这些市民每次如此辛苦地远道而来浦东机场接我，还要受到警察的打压。<br />
我走到机舱口，就看见等候我的几位警官。我们彼此已熟悉，都知道对方的来意，见面就像老朋友一样地问候一下，一位边防警察上来轻轻挽住我的左手，跟随他们走出与飞机联接的通道。通道口又有十几个人等候，边防警察穿制服，其他警察都是便服。我的出现，他们也活跃起来，有的拍照、有的摄像，前面开路，后面守卫，但是气氛已没有上次那么热烈而紧张，“卡擦、卡擦”的声音也不频繁了。其实，以后一两个人来迎接我就可以了，这样可以节省纳税人的许多钱，我不是恐怖分子，没有他们领导想象的那么可怕，是一个要求回国回家的普通公民。<br />
很快我们走进我熟悉的会议室，在这里我已好几次被搜身查物。室内有十几位穿制服的警察，其中有几位坐在里面的一间。我一进门，就被安排坐在沙发椅子上，两位警察有模有样地站立着向我宣布法规第几条第几款，意思就是他们要依法检查我的人身及物品，请我配合。我马上起立让他们搜身，并对他们说：“你们按法律程序办，我乐意配合。行李都在这里，你们慢慢仔细地检查吧。这次我特意少带一些东西，让你们检查时方便一些，可以少用一些时间。”这些东西上次都检查过，但他们还要例行公事，每一件每一页要拍照摄影。笔记本电脑是他们最感兴趣的物品，里屋的警察就在折腾电脑、优盘、电子辞典这些玩意儿。<br />
我坐在沙发上一边喝警察送的瓶装矿泉水，一边欣赏他们的检查工作。刊登苏灵《冯正虎回国》一文的这期新华时报，我随身带着。警察对文字东西特别感兴趣，一看到苏灵的文章，又是复印，又是拍照摄影，一定会报送领导审阅。他们检查完毕，要求我在一份《中华人民共和国浦东出入境边防检查站检查笔录》（编号：0550007950）上签字。检查笔录记载的内容如下。检查时间为2009年7月31日20：50至21：10。检查对象：冯正虎的人身及行李物品。检查人员：浦东出入境边防检查站审查队民警瞿向东（人民警察证：035391）、奚叶（035334）。检查过程及结果：在检查过程中，被检查人情绪稳定，配合检查；未发现其他可疑物品及证据材料。我在检查笔录上签名，并抄写了一份留下。警察还要求我在检查记录上盖手印，遭到我拒绝，我要求他们出示相关的法律规定，后来警察的领导示意不要盖手印了。<br />
我还向边防警察的领导投诉：“上次我回国时你们检查后归还我的东西未清点，结果上飞机发现我笔记本上抄录边防警察检查单、记载国内通信地址的页面被撕掉，记载通信地址的小本、一本收录国内律师名片的名片簿也不翼而飞，电脑的数据被删除。边防警察认为，他们肯定不会干这些事。我相信他们的保证，并对他们说：“我知道国内的情况，现在穿制服的警察还是规规矩矩的，但是脱下警服的人就无法无天，会做这些偷偷摸摸的事，肯定是晚上看守我的便衣警察干的。其实，他们没有这个必要，这些东西检查时已经复印拍照，他们需要问你们拿就可以了。这些人或许是心态不正吧。”边防警察笑着不作回答，大家心里都明白。<br />
接着，这群边防警察的领导余警官（人民警察证：034253）又一次向我宣布：“根据上级领导指示，还是不让你入境回国。”我问他：“你这个上级领导究竟是谁？这样重大的处罚怎么可以空口无凭，连一张正式文书都无法出示呢？你们的行为是违法的。”他也无法答复，只是苦笑。我知道，与这些警察论理是无济于事的，他们没有权力去纠正上级领导的错误，他们需要拿工资养家糊口。我又一次被移交给便衣警察监管，等待明天被遣送回日本。全世界都懵了，我究竟是中国国民，还是日本国民？<br />
<br />
<br />
二、&nbsp; &nbsp; &nbsp; &nbsp; 回国回家---妈妈的呼唤<br />
<br />
当晚21：20分左右，我走出搜查物品的会议室，就有三位黑衣人来接我。一个人开路，一个人在我左边挽着我的左手，另一个人在我右边挽着我的右手，夹着我并排同行。下楼后上了警车，我坐在后排夹在两个黑衣人中间。我坐的警车后面，有两辆警车、一辆面包车跟随，一同驶向浦东机场内部的宾馆。其中一辆警车的标记是边检，另一辆警车的标记是市局，表示上海出入境边防检查总站与上海市公安局在共同行动。此时，看守我的便衣警察是隶属于上海市公安局。<br />
我一路行走，摄像师跑前跑后的陪伴，这批人一直护送我到上次回国住过的同样一间客房内。我一进房，就打开手提箱，取出在日本机场上买的瓶装饮料、日本点心、一本书，洗一下脸，倚靠在床上休息，看书喝饮料吃点心。我热情招呼这些警察也来尝尝这些异国风味的小点心，但是他们不肯尝味，或许是他们的纪律吧。其实，我与这些警察的领导的领导有矛盾，正在针锋相对、寸步不让，但与这些普通的警察一直友好相处，我尊重他们的人格，理解他们执行公务的难处，除非个别故意刁难、折磨我的警察，我就会动用我的智力、关系及法律手段让他尝足苦头。<br />
这次三个黑衣人也是寸步不离，但是这些人明显不如上次的黑衣人那么专业，动作规范，不看电视、不睡觉，与被看守人保持距离又寸步不离，而且长相衣着很精神，黑短袖黑裤子黑皮鞋，一副训练有素的派头。这次的黑衣人有点冒牌，穿着印着品牌商标图案的黑短袖，不是黑皮鞋，而是黑跑鞋，坐在床边上腿不由自主地一直抖动，一幅吊儿郎当的腔调，看上去不像正宗的特警，不是国保警察，就是保安公司借来的保安人员。他们坐着陪夜，肯定忍受不住。深夜里，三个人轮流在另一个床上睡觉，其他两人在看电视，一直播放到早上，有不少精彩的电影，我睡了一会儿也索性起来与他们一起观看电视。<br />
晚上22：30，国保警察老沈来看望我，当然是领导指派他来的。我们彼此问候后，就进入主题对话。他问我：“我们找了你妻子、哥哥都谈过，你妹妹最近回国，我们也与她谈过，让她们劝劝你。她们怎样告诉你的？对你不能回国的问题有何看法？”我回答：“她们告诉我的，与你们与我谈的内容一样，也是这些老的东西翻来覆去。我们家属的态度很明朗：回国回家，全家支持；卖国投敌，一致反对。” 其实，老沈、所有的警察以及躲在背后发号施令的领导只要回家问问他们的妈妈，就可以知道我所有亲属的态度，妈妈肯定会说：天下哪有不让孩子回家的道理，孩子不听话，回家可以处罚。<br />
老沈说：“我们已经看到支持你回国的公民呼吁书，还有法律意见书征求意见稿。有的部分写的还可以，有的部分写的不妥。”我说：“是的，你们的领导肯定不满意的，我在批评他们。这个与理与情与法都相背离的事肯定做不长。我希望，上海的领导现在改错还来得及。”老李说：“你懂法律，但你搞错一个关系，是边防站不让你入境回国，边防站不属上海管。”我告诉他：“我知道，边防站不属上海管，但上海当局要指使边防站禁止我入境回国的事也是很容易做到的。你们的说法欺骗外国人可以，但我这个长期在国内生活的人是不会被蒙骗的，我了解我们国家的特色，知道地方当局的权势。你们可以把我从北京绑架到上海，非法拘禁四十一天，而且没有任何法律凭据。如果按正常的法律思考，地方当局敢做这样吗？按照法律，我们的法院都是独立的，但是事实上地方当局都在指使法院违法。例如，法律规定法院受理立案七天限期，但是民告官的案子，法院会永不理睬。没有多少在外国的人能看懂中国的实况。”<br />
老沈关心的问题很多。我状告航空公司的诉讼，日本法院是否已受理？我是否参加热比娅来日举办的会议？我是否已向北京中央上层写信控诉这起事件？等等，我都如实一一回答。与我谈了一个小时左右后，他就离开了，我继续与三个黑衣人做伴，睡觉休息。虽然我与母亲、妻子不能相见，但我们的距离已很近，同在一个上海市，在今晚的梦中相聚吧。<br />
<br />
<br />
三、&nbsp; &nbsp; &nbsp; &nbsp; 日本航空公司能保持中立吗？<br />
<br />
第二天上午9：00左右，一位航空小姐在边防警察的陪同下，来到我的客房，拿着一张登机牌，对我说：“冯先生，这是你的登机牌，9：50分登机，座位11A。你确认后，我先撕去的这小半张，其余部分登机时他们会给你的。”我说：“知道了。这张回程的机票也算我自己买的吧。”我这次回国的机票是双程票，机票上确认的回日本日期是8月7日。现在，上海边防警察强迫让我提前回日本了。<br />
机场地区下着倾盆大雨。上午10：10左右，在十几位警察、三辆警车的卫护下，我抵达机场内的登机通道门口，旁边停着等待起飞的全日本航空公司NH0922航班。两位黑衣人左右挽着我的手从警车里出来，我站在登机通道门口向围观的许多警察、机场工作人员，对着摄像机再一次声明：“我是被强迫离开中国的。上海当局应当立即纠正错误。没有理由、没有法律依据，随意禁止一个中国公民、上海市民回国回家，这是违法犯罪。我还要继续回国。”上了登机通道的楼面，昨天向我宣布禁止入境的余警官把我的护照、一张NH0922航班的登机牌交给我，告知两位黑衣人只要送到机舱门口就可以了。我是最后一个上飞机，不久航班就起飞了。<br />
我是第三次乘全日空的航班。第一次是6月7日，也是我第一次被上海当局禁止入境回国，警察用全日本航空公司的航班强制遣送我回日本。我至今还不清楚，上海当局为什么要利用日本航班，而不是用运载我回国的中国国际航空公司的航班或其他航空公司的航班，而且用日本航班在漆黑的夜晚里把我抛弃在一个远离东京而陌生的日本关西机场上。是否让我这个中国知名的维权人士去愤怒日本？的确，我一回日本，就向全日本航空公司的总经理提出书面投诉，指责全日空公司违背国际公约及航空公司关于办理登机手续的规定，协助上海警察非法强制送我回日本，并要求追究责任。后来，全日空公司的部长亲自复函：全日空公司上海浦东机场的工作人员当时以为是经过我本人同意，自愿回日本乘全日空航班的，并请我谅解。全日空公司的辩解理由不存立，但态度很诚恳，我就没有继续追究它的责任，也没有为难它。<br />
我清楚中日关系，更知道中日两国民众的情绪，所以我在这起将会牵连日本的中国公民被禁止回国的事件中谨慎出牌，保持高度的社会责任感，不会像上海当局如此荒唐的极不负责任的胡乱作为。我一至六次回国闯关都没有动用日本航班，现在要启用日本航班了。全日空公司对我这个特别乘客也很谨慎，我的姓名已在它的客户系统里备案了。7月31日下午，我乘NH0921航班办理登机手续时，服务员小姐把我护照一扫描，就出现警示信号，停止办理登机手续，她就电话通知上级，并让我们等着。不久，当班负责的女士来了，请服务员小姐继续办理，并礼貌地向我们致歉，最后将登机牌交付我们。8月1日上午，浦东机场的全日空服务员小姐亲自上门，让我本人确认回程的登机牌，而没有容许上海边防警察擅自代办。<br />
全日本航空公司是否能保持中立？还要看下一次。我不知道，上海当局是否已经给它压力，要它也像中国国际航空公司、美国西北航空公司一样配合上海当局侵犯中国公民的回国权呢？但是，现在这盘棋已越来越明朗，日本公司应该不会自找麻烦。中国国际航空公司、美国西北航空公司将要在日本被起诉，日本的国土交通省（相当于中国的交通部）已立案调查，它们丑闻也被日本的主流媒体广为报道；美国西北航空公司在成田国际机场的大庭广众之下被我们抗议谴责，它的违法行为还要接受美国本土民众的谴责与官方调查；中国国际航空公司的行为也是违反中国法律的，它也会受到国内官方部门的调查与民众的谴责。<br />
企业不顾企业伦理，甚至违法投靠政府部门，是为了谋取更大的商业利益与经营方便。但是，现在这些伤害我的企业已暴露丑行，他们不再会从上海当局这里拿到好处，政府方面会把责任推却得一清二楚。这些商人想拍中国地方政府的马屁或惧怕中国政府，这也是可以理解的，但是他们也不想一想，连中国政府都怕得不敢让他回国的人，他们可以欺负的吗？可以以此来邀功的吗？这是一个会拼命的人，而且是得到中国民众与法律支持的人。实际上，中国法律制度已基本建立，民众就是利用法律抵抗官府的侵权与压迫，现在的中国已是官怕民。与中国交往的外国公司应当依法经营、保持中立，才可以顺利运营，否则麻烦不断。<br />
<br />
我还会继续一次一次回国闯关。我的回国行动不仅是为了我个人可以早日回国，而且还向全世界表达了中国公民的意愿：中国是中国人大家的，回国回家是中国公民的基本自由权利。我已在致美国总统奥巴马的信中写道：“我争取回国的行动不仅仅表明我有勇气，更重要的是表明我对中国发展前途的信心，对法律的信心，对人权价值观的信心。”我深信，回国闯关的次数已不会很多，不久我就可以入境回国。<br />
<br />
<br />
2009年8月7日东京</div>

							
							
							
							
