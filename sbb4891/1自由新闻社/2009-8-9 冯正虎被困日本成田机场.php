													<h2>不让冯正虎回国是我们的国耻！ / 翟明磊</h2>
						
						
																											<div id="postmessage_329968" class="t_msgfont"><a href="http://www.my1510.cn/article.php?id=eed7276003bd2f58" target="_blank">http://www.my1510.cn/article.php?id=eed7276003bd2f58</a><br />
<br />
（虽然受到上海警察的电话警告，我仍愿意写下如下的文字）<br />
<br />
 <br />
<br />
你,一个守法的中国公民，开开心心地走过海关,告别家人,去了美国,加拿大,……度过一个假期，或者处理完一个外贸事务，突然间你发现，你不能回到中国了，你见不到国内的家人了。你的国家无缘无故不让你回国了！<br />
<br />
 <br />
<br />
不要以为是天方夜谭，这种荒唐的事就发生在上海人冯正虎的身上。――也许――明天――就可能――发生在你的身上。<br />
<br />
 <br />
<br />
冯正虎先生持有中国护照，在日本短期停留后，上海政府竟然不让他回到上海的家，没有给出任何理由。冯正虎说：如果我是通辑犯，我回国，正好来抓我，如果我是政治犯，你应当给出驱逐出境的命令，可是他什么都不是，既不从事政治活动，也无经济犯罪或其它不法行为的嫌疑，堂堂一个中国公民，竟然回不了自己的国家，没有任何理由！<br />
<br />
 <br />
<br />
不让他回国的上海有关部门没有出示任何书面的东西，只是口头上说，领导决定这次还是不让你回来！<br />
<br />
 <br />
<br />
我认为不让冯正虎回国是我们的国耻。<br />
<br />
 <br />
<br />
在现代社会，在现代化的中国，国耻不再是丧权辱国，割地赔款。国耻是一个现代化国家不能平等对待他的国民。<br />
<br />
 <br />
<br />
1984年联合国《世界人权宣言》第十三条,第二款明确规定：“人人有权离开任何国家,包括其本国在内，并有权返回他的祖国。”如果我们的中国护照不能保护自己的国民正常地回到中国，这不是我们国家的耻辱吗，这不是外交丑闻吗？<br />
<br />
 <br />
<br />
冯正虎先生已经是八次闯关要求回到中国,每次都是没有任何理由的拒绝。如今他睡在日本东京机场的入境厅的长椅上，并声明放弃日本签证，拒绝回到日本。已经是十二天，前面七天，他没有吃的没有喝的，只能以自来水维生。我不知道他是什么心情，一个不能回到自己国家的公民。一个上演了好莱坞电影《幸福终点站》真人版的悲剧人物。<br />
<br />
 <br />
<br />
他为什么会成为国际弃儿呢，因为他在上海有效地用他的才识帮助了访民，拆迁户，受到不公正待遇的百姓维权，并主办《督查简报》用文字形式传播上海某些官员腐败不公的情况，他还编辑了上海法院司法不公的案例集。这是上海政府某些官员用国家的名义行某官员之私。用给国家抹黑的方式来阻止优秀的公民回国。这不是新国耻又是什么呢。<br />
<br />
 <br />
<br />
日本媒体报道冯正虎事件后，国际的空姐们走过时纷纷向冯正虎微笑致意，每天有上万名过境游客看到冯正虎用英文，日本书的抗议背心，这个丑不是丢大了吗？<br />
<br />
 <br />
<br />
我们看在眼里急在心里，我们的国家，这样的外交丑闻！一个国家竟然没有任何正当理由，便不让他的公民回到国家。可是有些人是不急的，仿佛我们的国家和他们没有关系！<br />
<br />
 <br />
<br />
冯正虎正在用他的方式训练我们的政府，让他们明白，国家对公民应当是个什么关系：一个国家是不应当耍流氓的，否则他便失去了他的威信，即使有多少GDP，也会被别国耻笑，称之为野蛮国家。因为这个国家既不懂人权法，也不知联合国人权宣言，也不知道现代国家的游戏法则。<br />
<br />
 <br />
<br />
这不是新国耻吗？<br />
<br />
 <br />
<br />
冯正虎回不了国，我们任何一个公民就都有可能在没有任何理由的情况下回不了自己的国家。不让冯正虎回国是我们的国耻，没有任何一个国家会这样对待自己的公民。<br />
<br />
 <br />
<br />
我一直在思考国家与公民究竟是什么样的关系，为什么这么多人谈爱国，却越来越多的中国人愿意移民。在《礼记》这本书中,写道古代的中国人如果离开他的母国,要在国境线上找一块荒地住上三个月,披散自己的头发,每天向着祖先坟墓的方向痛哭!他的马儿不能梳理毛发,他不能洗浴,不能歌唱,只能以泪洗面!三个月后，他才可以离去，永远地离去。<br />
<br />
 <br />
<br />
这是一个古老中国人离开他的祖国时的情感。可是我们多少人拿到移民的批准函是高兴地跳起来，乐不可支。是什么样的国家让他的国民这么兴高采烈地离开。我曾经不理解我那些移民的朋友们，直到有一天。<br />
<br />
 <br />
<br />
有一天，我收到龙应台基金会的邀请，请我到台湾访学二个月，感受台湾，于是我准备起来了，我花了五六百的士费跑了上海各个部门，办了无犯罪纪录等等各种证明，最后他们要我出示不是法轮功的证明，我说“你们如何证明我是法轮功”，他们说“你如何证明你不是法轮功”。（另外说明一下，我的确不是法轮功练习者，但对法轮功并无歧视。）于是按他们的要求我去了户口所在的小区居委会开证明，这个居委会说，“你不在这个小区居住，我们不能开，”我去了我居住的小区居委会，他们又说，“你刚住进来一年，我们要观察半年再开”，我又去了以前住了八年的小区，这个小区居委会说：你户口已经不在这儿了，我们不能开。于是我开不出“我不是法轮功”的证明。于是我去不成台湾。在办证路上，突然间我涌出了泪水与一个念头：移民吧，移民吧。<br />
<br />
 <br />
<br />
我忏悔，那一瞬间，我真的是这么想的，那一瞬间，我同移民的朋友们和解了，我体会他们的心情：T君，北京大学国际金融毕业高材生，研究生名额被高干子弟挤走了，在上海找银行工作却因为没有上海户口被拒，只能在农村的工厂中做工，最后被哈佛以高额奖学金录取了……Z君，受不了空气污染与单位的勾心斗角，去了新西兰的朋友们……J君，被公安追捕，只因坚持独立思想与写作的诗人们，他偷渡国境，在丹麦落脚，却被北欧人民视为文学财宝，丹麦政府免费支助他出书，写作。<br />
<br />
 <br />
<br />
我对把孩子生在美国与香港的朋友们微笑，我对被困在成都不许到香港的冉云飞微笑……我悲极生乐，为那些自由人微笑！<br />
<br />
 <br />
<br />
真的，一个不能平等对待国民的国家，一个处处把国民当罪犯的国家，你如何爱他呢。所以我说过一句话：我爱中国，我不爱中华人民共和国。是的，我不反对国家，我也不会颠覆他，我建议所有的朋友们遵守国家的法令，和平建设国家。但我真的真的没办法爱这个国家啊！请原谅我吧！<br />
<br />
 <br />
<br />
冯正虎在日本留学，工作，有无数次机会成为日本国民，但他没有，他想回到自己的国家，为国家尽一份力，尽一份心。可是他的国家不让他回国。我又想起我的一位朋友，那位勇敢揭开公司假帐的总经理，MBA王惟尊，（他因此而坐牢，当时有三百MBA与吴敬琏先生为他呼吁，使他平安得救，）回报他的不是鲜花而是与非法企业主勾结的北海公安局的通辑令。他被迫流亡国外。<br />
<br />
 <br />
<br />
我们的国家怎么啦，为什么这么优秀的人才要被赶走，为什么那些不愿意移民的热血汉子得到这样的下场！而爱国者被称之为叛国者！<br />
<br />
 <br />
<br />
我在读一本《父亲的战场》的书，这是写滇西战场中国抗日远征军人的书，我很喜欢作者章东磐的一句话：“一个男人，千万别轻易说爱。一旦说了，就要有为爱而死的勇气，无论爱的是你的祖国，还是女人。”<br />
<br />
 <br />
<br />
我相信冯正虎在成田机场的举动说明了他就是这样一个男人。<br />
<br />
 <br />
<br />
冯正虎十二天没有洗澡了，他疲劳地孤单地睡在日本国入境厅的长椅上，我仿佛看到了上千年前，那些离开母国士大夫的影子，他们同样披散头发，不洗不浴，不吃不喝，来安慰自己痛苦的心灵，那些不愿离开国家的优秀的灵魂们！我们的国家病了吗，疯了吗。<br />
<br />
 <br />
<br />
2009年11月15日星期日<br />
<br />
 <br />
<br />
第十二天了，原谅我没有更早地发出声音，冯大哥！实在是因为害怕，我们比其它人更需要国际交流的机会，而他们随时可以不让我们出境交流，这是许多朋友更愿意默默支持你的原因。这也是我在台湾出访问题上被不公正对待后保持沉默的原因。当我签名支持你时，就受到了威胁。是你的行动唤起了我，你在为自己争权利，也在为我们争权利，你是一个人的战争，身后却是亿万中国人，如今,我无所谓了。最多和香港的朋友们说一声永不再见，国境从来不是为自由的心灵所设，正如《世说新语》，“礼岂为我辈设也”，――“国境岂为我辈设也”。</div>

							
							
							
							
