<br />
作者：冯正虎 文章来源：维权网 点击数：156 更新时间：2009-11-7 11:42:52<br />
<br />
我是中华人民共和国公民（护照号：G33406155）、上海市民（国内身份证：310108195407012452）。在中国的户口居住地是上海市。<br />
<br />
我在日本持有“人文知识、国际业务”资格的工作签证，可以自由往来日本，并在日本有限期的居住。但是，自从我6月7日被上海当局无法无理的禁止回国起，我已无法从事中日之间的国际业务，只好辞职，我在日本已无经济收入来源，日本的工作签证对于我已失去真实的意义。<br />
<br />
现在，这份2010年6月12日到期的日本工作签证已成为我回国的障碍物，也是中国上海当局利用非法手段禁止我入境回国的障眼法。利用企业为了经济利益屈服权力的弱点，上海当局可以轻易地要求航空公司拒载或参与非法绑架，不惜一切非法手段将我强行滞留在日本，又以我在日本有签证的借口可以瞒上欺外，企图掩盖他们的违法事实。或许，日本政府也是这个原因而一直默认。没有航空公司的配合，中国上海当局是根本无法可以做到：连续八次将一个合法的中国公民强制滞留在东海彼岸的日本而禁止回国。<br />
<br />
2009年11月2日我乘全日本航空公司NH0921航班已回国，虽然没有办理入境手续，但已在中国本土，晚上住在上海浦东机场宾馆。第2天，即11月3日上午约9：45，十几个违法的上海警察又将我强行送至全日本航空公司NH0922航班的停靠处，企图又一次非法“遣送”一个本国公民去外国。我强烈抗议，他们使用暴力手段将我绑架至飞机上，我竭力抵抗，死守飞机的登机口，与这些绑匪搏斗了近二个小时。最后，全日本航空公司上海经理饭田屈服于绑匪的威胁，协助他们的暴力绑架行动，四个年轻力壮的便衣警察硬将我拖至机仓底部的座位，全日本航空公司的一位身材高大的上海职员也与我搏斗，并将我压在座位上，这时我已经筋疲力尽，无力抵抗他们的违法犯罪行为。航班上乘客亲眼目睹他们的暴力绑架场面，飞机也由此延误一个多小时起飞。我第一次被非法强制遣送回日本，被抛在远离东京的关西机场，也是这家日本航空公司，当时我还谅解了他们不知情的违法做法。<br />
<br />
中国上海当局的暴力绑架行为，在航空公司的配合下，又一次得逞了，把一个中国公民从中国上海运载到东海彼岸的日本，但是这次也使我回日本的心死了，我不会屈服暴力压迫。我已有八次被拒绝入境的经验，我清楚，如果这次我一旦进入日本后，日本政府会继续默认下去，航空公司即使赔款也不敢再运载我回国，中国政府也会继续默认，上海地方当局的犯罪分子可以逃避法律追究，而美丽的日本将成为我的牢笼，或许从此我将永远离开我的母亲、妻子，也无法在自己的祖国土地上尽责，同时还有更多的中国公民还会继续遭受基本人权―回国权的侵犯。所以，我这次决意不入境日本，要求返回中国，作为中国人也需要做人的尊严，因为被上海当局伙同日本航空公司绑架到日本，这是中国政府的耻辱，也是我个人的耻辱。<br />
<br />
2009年11月4日被绑架到日本，至今11月7日没有入境日本，三夜四天住在入境审查大厅，晚上躺在长椅上，白天忍受饥饿的折磨。日本成田机场出境的大厅及通道上有很多商店及吃食店，还有饮料的自动贩卖机，但是入境大厅及通道什么也没有。我三天仅吃了三个饭团，当我正式向成田机场出入国管理局的承办官员铃木先生提出，希望他从人道的角度请入管局的职员代我购买几个饭团，但遭到拒绝。而且，我妹妹送来的食品，他们也拒收了。他们企图通过饥饿的变相虐待方式逼我入境日本，这是不言而明的。但我会坚持下去，因为我清楚，日本官僚并非人道，不仅冷漠，甚至有点残忍。或许，友爱的国家仅是日本鸠山总理的理想而已。如果这些事发生在中国，中国人决不会这样对待外国人，中国人内斗很残忍，但对外国人总是很客气，至少不会加害困境中的外国人，耍这些小儿科的害人伎俩。国不和，外人欺，这是中国的宿命。<br />
<br />
这半年来，我竭力在避免这个结局，但还是无法回避。我与中国政府、日本政府都是失败者。我无力抗拒上海当局的暴力，只好被逼在日本的国门外穿上抗议的广告服，忍受着饥饿，面对成千上万进入日本的各国旅客，抗议上海当局与航空公司违法行为，呼吁中国政府、日本政府以及公众关注我作为中国公民的回国权。我请求中国政府履行国家的责任，依法保护中国公民的回国权，追究在这起事件中违法者的法律责任。我请求日本政府督促参与绑架我到日本的航空公司，送还我回国。如果日本的官僚有点人道与友爱精神，请不要在我处于困境中再加害我，我自己出钱请管理我的入管局官员代买一点食品不会是违反日本法律吧，日本政府不要光说友爱，应当有点最起码的人道。<br />
<br />
我已口头向成田入管局铃木先生提出放弃日本签证的请求。现在，我将正式委托日本律师提出我的书面申请，并交还我的外国人登录证明书。我与日本有近二十年的交情，做了许多推进中日交流与友好的事，但最后我将以断绝的方式告别日本，这是很遗憾的，也是不得已的选择。<br />
<br />
祝愿日本成为一个友爱的国家。祝愿中日友好。<br />
