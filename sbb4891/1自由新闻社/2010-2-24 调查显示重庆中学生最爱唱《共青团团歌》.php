　　 今天，华龙网一篇题为《调查出人意料：重庆中学生最爱唱〈共青团团歌〉》的新闻报道迅速窜红各大网站。文章引述了重庆市教科院德育研究中心最新出炉的《重庆市青少年课余文化生活调查报告》的有关内容――“学生们最喜欢唱的歌曲是《共青团团歌》，还有《国歌》、《同一首歌》、《歌唱祖国》、《团结就是力量》等歌曲。”而对于这一现象形成的原因，文章又引述了有关专家的分析――“2008年开始，‘唱红歌、读经典、讲故事’的校园文化活动对全市中学生情感态度价值观的形成产生了较大影响。”<br />
　　<br />
　　 不过，对于重庆市教科院德育研究中心的调查结果和有关专家的分析，可能连报道该新闻的记者他自己都不太敢相信，所以才在文章的标题中加了“调查出人意料”几个字眼。<br />
　　<br />
　　 那么，为什么中学生最爱唱《共青团团歌》出人意料？这样的调查结果究竟出乎谁的意料？<br />
　　<br />
　　 第一，难道《共青团团歌》没人爱唱才在情理之中吗？其实，我倒是愿意相信中学生最爱唱《共青团团歌》。10年前，我也是一名中学生，准确的说是高二的学生。虽然初二就入了团，但是直到高二，被同学选为团支部书记的时候，我都还不知道团歌是什么。倒不是我孤陋寡闻，当时不要说会唱团歌，同学中连团歌是什么都不知道的大有人在。后来，我自学了团歌，还在过团组织生活的时候，教会了全班同学唱团歌。当然，他们爱不爱唱团歌我就不知道了……毕竟10年过去了，也许情况已经有了很大改观。谁说现在中学生就只喜欢那些“靡靡之音”，而对团歌之类的革命歌曲就不感兴趣呢？<br />
　　<br />
　　 第二，认为“中学生最爱唱《共青团团歌》出人意料”，这种认识本身就值得批判。不是我上纲上线，既然中学生加入了共青团，作为一名团员，爱唱团歌是一件很正常的事情，有什么好大惊小怪的呢？入团的时候都写过申请书，入团的时候也都宣过誓，哪个人不是写得深情款款的，哪个人又不是吼得铿锵有力的？难道都是在放屁？抛开政治上的倾向不谈，做人起码应该做一个诚实的人，总不能一边盼望着入团，一边又盘算着共青团早点跨杆儿吧，这样未免有点不道德。再说团歌也是歌，歌词和旋律也挺优美的，中学生爱唱怎么就出人意料了呢？<br />
　　<br />
　　 第三，中学生最爱唱《共青团团歌》这个调查结果是怎样得来的？我想，这才是问题的本质，也是大家质疑最多的地方。当然，从新闻报道上并看不出来。但是，对这个调查结果的科学性，我是有所保留的。关于青少年课余文化生活的调查，一般都是通过发放调查问卷的方式进行的。那么，问卷的设计就很关键。调查问卷的问题不外乎开放式和封闭式两种，也就是主观题和客观题形式。比如说，设置开放式的问题，问中学生爱唱什么歌。那么回答可能是五花八门的，很难统计。而相对简单的方法就是设置几个选项，比如《共青团团歌》、《国歌》、《同一首歌》、《歌唱祖国》……然后再来个兜底的“其他歌曲”，让被调查者自己填，这样统计起来就方便一些。但是，这种开放式和封闭式相结合的问题设置方式，往往不利于反映被调查者的真实想法，他们很可能胡乱地选择几首歌了事。这样统计出来的结果又能说明什么问题呢？<br />
　　<br />
　　 因此，我认为真正出人意料的不是中学生爱唱什么歌，而是居然能调查出来中学生爱唱什么歌。起码，我认为通过发放调查问卷的方式，是难以准确调查出来的。作为一个研究中心，发布这样的调研结果，未免有点不负责任，而更可悲的在于学术研究似乎又一次沦为拍马屁的一种工具。<br />
　　<br />
　　 也许，这一次，重庆的中学生着实“被最爱唱”了一回。<br />
