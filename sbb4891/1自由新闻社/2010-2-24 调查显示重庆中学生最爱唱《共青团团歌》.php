课余时间中学生爱唱什么歌、爱读什么书、又爱看什么电影电视剧呢？调查结果让不少人意想不到。学生们最喜欢唱的歌曲却是《共青团团歌》，还有《国歌》、《同一首歌》、《歌唱祖国》、《团结就是力量》等歌曲。<br />
<br />
　　“2008年开始，‘唱红歌、读经典、讲故事’的校园文化活动对全市中学生情感态度价值观的形成产生了较大影响。”专家分析说，从阅读数据中可以看出，孩子们喜欢中国和世界的经典书籍，但他们读的书多数是故事和小说类，对政治和历史的关心度不够高。“国产精品影视剧受到中学生的普遍喜爱，中学生最喜欢和经常看的电视节目是他们价值取向和心理需要的反映。”<br />
<br />
　　中学生课余生活都干了些啥？调查结果显示，主要是看书、做作业、补习功课。<br />
