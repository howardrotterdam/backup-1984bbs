标签：杂谈&nbsp; &nbsp; &nbsp; &nbsp; 分类：政论<br />
&nbsp; &nbsp; 古代帝王、诸侯、卿大夫、高官大臣等死后，朝廷根据他们的生平行为给予一种称号以褒贬善恶，称为谥或谥号。<br />
&nbsp; &nbsp; 比如：汉文帝刘恒谥号全名为“孝文帝”；恭亲王奕欣的谥号为“忠”；曾国藩获谥“文正”。<br />
