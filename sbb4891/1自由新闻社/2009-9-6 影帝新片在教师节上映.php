　　新华网报道，9月4日上午，温总理一大早就来到北京市第三十五中学，在初二(五)班和学生坐在一起连上5节课，对当前中学教育进行调查研究。温总理日理万机，在教师节来临之际，利用一天的时间专门看望教师，调研教育工作，并且用一上午的时间一连听了五节课，这五节课中有语文、数学、地理、音乐等。他还用一个小时的时间对五节课一一作了点评，老师们都认为总理的点评很到位，特别受启发。我认为，温总理亲自到教室里听课的务实作风，具有三大现实意义。<br />
　　一是体现了温总理对教育工作的高度重视和对广大教师的尊重。自担任共和国总理以来，每到教师节，Wen Jiabao都要抽出时间看望教师和学生。今年秋季新学期伊始，第25个教师节又即将到来。几天前，温总理在安排一周工作时，专门留出一天时间到一所中学听课，并和教师们座谈。他要亲身感受当前中学课堂教学的实际情况。这就说明了温总理始终在关心着我们的教育事业，关心着广大教育工作者。温总理始终把“百年大计，教育为本”放在心里，抓在手上。温总理坐在教室最后一排聚精会神地听老师讲课，一方面反映了大国总理的谦虚的胸怀，另一方面也看出了温总理尊重教师劳动、亲近和爱护学生之情。这是教师的光荣，也是学生的幸福。<br />
　　二是体现了对当前教育改革的极其关注和检验。推进素质教育，全面提高教育教学质量是教育改革的主题。近年来教育改革的成果究竟怎样，课堂教学是检验这一成果的最好平台。要想知道梨子的滋味，就必须亲口尝一尝。温总理一连听了五节课就是亲口尝梨子的滋味。听课之后，他用一个小时的时间进行点评，肯定了教学的成果做法，也指出了教学中的不足之处，这既是对课堂教学的评估，也是对整个教育改革的评价，对今后一个时期的教育教学改革必将起到巨大的推动作用。可以说，这是整个教育的一件盛事。<br />
　　三是给各级干部如何重视教育作出了榜样。教育是百年大计，教育强则国家强。各级政府和社会都必须高度只是教育，真正形成尊师重教的良好氛围。但是，我们的一些地方，重视教育只是挂在嘴上，没有设身处地、扎扎实实地去了解教育、研究教育，发现问题、解决问题。对教育工作的指导也只是浮在面上，不深入、不具体，缺乏实际意义。温总理却能够深入到课堂中去了解教育，研究教育，基层的一些官员却做不到，与温总理相比一定会感到汗颜。从这个角度讲，温总理听课也给我们的官员们上了一堂生动的思想教育课。温总理的身体力行的示范效应，必定会推动各级官员和全社会对教育的更加重视和支持。<br />
　　总之，温总理这次“当学生”具有重大的现实意义，这是中国教育之福，也是教师之福，学生之福。最终就是国家和民族之福![稿源:华声在线] [责编:黄炜信] [作者:山青水秀]<br />
