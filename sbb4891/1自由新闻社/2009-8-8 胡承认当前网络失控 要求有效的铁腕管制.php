													<h2>胡承认当前网络失控 要求有效的铁腕管制</h2>
						
						
																											<div id="postmessage_195889" class="t_msgfont">(博训2009年08月07日发表)<br />
<br />
博训记者独家报道：上个星期，上海、江苏、湖北、四川、广东等省市同时召开全省新闻工作会议，会议由省委书记亲自主持，传达中共中央关于媒体工作的最新指示。据称，中共中央日前在京召开了近年来最高规格的媒体会议，会议由Hu Jintao作报告。报告主要涉及到中共对互联网等新媒体的看法和对策。<br />
<br />
Hu Jintao在会议上讲，我党对传统媒体的管理一直以来是行之有效的，现在我们要改革传统媒体，但管理不能松懈。但在随后以大篇幅讲到的对互联网控制上，Hu Jintao就没有那么自信了。他说，互联网这个新东西，有太多的未知数，“我们已知的”很有限。<br />
<br />
Hu Jintao说，如果从过去一段时间的情况判断，我们对互联网的管理“进退失据”。他严厉地警告，我们很多干部还没有认清形势，对互联网上的舆情和民意不当一回事，掉以轻心。他说，很多地方闹的事件，就是地方当局没有及时重视互联网民意，错误处理造成的。他说，目前，上下都要齐力，寻找“有效的管理、引导互联网的方法”。<br />
<br />
据传，中共内部一股强大的力量在抬头，他们要求严厉管制和打击互联网，甚至不管经济损失，要以“血腥的手段”镇压互联网“骚乱”。但胡温政府目前仍然在犹豫。Hu Jintao在会上也说，我们目前已经采取了一系列措施加强管理，要密切关注结果和效果。<br />
<br />
北新办一位领导这样解读这段话，关掉微博饭否，屏蔽微博推特(twitter)，关闭中国最大的学术和时评网站天益，逮捕刘晓波、关闭公盟，加强互联网审查等等，都是最近升级的对互联网打击的一部分。而博训稍早前报道的新疆关网，则被证实确实具有试验性质。新疆管网对经济的影响，网民的状态和心理，已经成为军队、国安调研对象，今后国内发生互联网事件，在有失控可能的情况下，不排除采取新疆模式，整个或者局部关掉互联网。<br />
<br />
目前全国都在召开以省委第一把手主持传达的媒体会议。据多个省份的新闻工作者透露，这是他们听到的中共中央最高领导人第一次坦承对互联网的管理有失控的趋势，他们都担心中央是否顶得住强硬派的叫嚣。<br />
<br />
有国家安全部高级官员向博训透露，即便失控的互联网也不会对中共的政权造成威胁，但他倒是担心，如果对互联网的处理不当，例如强硬派动不动就关网、抓人，倒有可能在当今干柴烈火的中国引爆燎原之火。他欣赏Hu Jintao的政策，加强管理和引导。他说，目前中共中央高层持此意见的占上风。不过，他也同时透露，中央高层有一派势力也在抬头，他们要利用互联网建立和民众的真正联系，纯洁共产党队伍，实行真正的政治体制改革。<br />
<br />
这位安全官员不愿意向博训记者透露这派的代表人物是谁，他说，你总有一天会看到这一派的，他们目前是沉默的大多数，但他们的力量不会太小。他最后说，未来中国的角力，第一波也是最激烈的一波，将会发生在中共和中共之间：有良知的共产党员将会向腐败的共产党员宣战。<br />
