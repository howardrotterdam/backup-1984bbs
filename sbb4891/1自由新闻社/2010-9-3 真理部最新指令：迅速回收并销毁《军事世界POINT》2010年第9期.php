　　另外，解放军太空监视网络的建设工作也在顺利进行中。费舍尔指出，解放军目前正在同时组建数个不同类型的光电和雷达监视卫星群。“遥感”系列卫星已经发射了多颗。而“环境”系列卫星也在去年9月份进行了第一批发射，这两颗均为光电小卫星，该系列卫星的第二批发射也会很快进行，这是一种合成孔径雷达卫星，该系列卫星最终将在太空形成完善网络。星座一旦建设完成，每天将会有多颗卫星飞越南中国海上空，足以满足对南沙海域的不间断监测。 <br />
<br />
　　不久的将来，预计还将有大批新型无人机加入解放军远洋监视网络的阵营。贵州航空工业集团和成都飞机工业集团都已经推出了高空长航时无人机的概念模型，在2006年的珠海国际航展上，贵航披露了采用盒式机翼设计的航程达7000公里的“Soar Eagle”概念机模型，而在2008年的珠海航展上，成飞集团的“Long Haul Eagle”更是横空出世，震惊四座，该机无论是外形还是尺寸均与诺斯罗普-格鲁曼公司的“全球鹰”如出一辙，在此次航展上，成飞集团还推出了尺寸稍小一些的高空无人机“天翼”，有消息称该机采用了先进的涡扇发动机，飞行测试工作已经开始进行。 <br />
