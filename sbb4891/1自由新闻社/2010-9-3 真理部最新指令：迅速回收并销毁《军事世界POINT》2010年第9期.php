<br />
张黎上将的提议 <br />
<br />
[ 转自铁血社区 <a href="http://bbs.tiexue.net/" target="_blank">http://bbs.tiexue.net/</a> ]<br />
　　香港《大公报》在刊登的一篇新闻报导中称，张黎上将呼吁在位于南中国海的、存在所谓“主权争议”的美济礁上修建海空保障设施。张上将是以全国政协常委的身份发表这一讲话的。他认为面对当前南海地区“非常严重的现状”，中国应当作出强硬反应。他指出，在南沙群岛的500余座岛礁中，中国大陆仅仅实际控制了4座岛，而越南则控制了29座，菲律宾和马来西亚也控制了至少4座以上的岛礁。他还指出，中国在南沙尚无石油钻探平台，而其他国家则早已开始广泛开采石油了。 <br />
<br />
　　为了保卫中国的利益，张上将提出了一个三步走的计划。第一步是大幅增加中国海军和其他海事监察部门大型舰艇和巡逻飞机的装备数量，以便更有力地维护中国在南中国海地区的利益。他强调应当配备一批排水量3000吨左右的护卫舰级别舰艇来执行此类任务；第二步是在南海地区组建一个“立体侦察观测系统”，目前中国在这方面的能力“还相当落后”；第三步是加大对基础设施建设的投资力度，在部分岛礁尤其是美济礁上新建一批港口和跑道。张上将认为：“这座岛非常适于修建港口和跑道，一旦建造工作完成，（中国）将能够控制整个南沙地区。”这意味着新机场的出现将大大提高解放军J-10和J-11战斗机的覆盖半径，这一点在张上将的讲话中被着重提及。 <br />
<br />
　　从张黎上将的讲话内容和过去以及现在所担负的职务来分析，费舍尔认为或许能够发现其背后所代表的真正意图。张上将的讲话是否代表了中国政府的未来政策走向亦或是对于现行政策的批评和指责？张上将的讲话以一句普通的官方用语作为开头，这或许反映了中国最高领导层的态度。他表示，“中国政府对于南海的规划和管理给予了极大的关注，胡锦涛总书记也多次对南海的规划和管理工作提出过重要的宝贵意见。”尤其值得注意的是在2008年4月初，胡锦涛总书记高调视察了位于三亚附近的中国海军新基地，并正式宣布中国海军071型新型两栖船坞登陆舰服役。这一事件表明，正如张黎上将所说的那样，胡主席非常清楚且重视解放军在南中国海所面临的问题。 <br />
<br />
　　张黎上将发表这番言论的目的是为了推动早已确定下来的计划和政策顺利实施，还是仅仅反映了中国高层强硬派对于在南中国海地区采取更加大胆军事行动的强烈愿望？很遗憾，依靠现有的资料我们无法得出一个肯定的结论。不过，张上将在发表其观点时的身份是全国政协委员，这一事实或许从一个侧面反映出了中国政府高层的支持，这番讲话随后便被刊登到了《环球时报》上面，并在互联网上广泛流传。总而言之，鉴于解放军正在努力增强其在南中国海地区的军事力量，费舍尔认为有必要将张黎上将的这番讲话当作中国政府所发出的一个严厉警告而认真对待。 <br />
