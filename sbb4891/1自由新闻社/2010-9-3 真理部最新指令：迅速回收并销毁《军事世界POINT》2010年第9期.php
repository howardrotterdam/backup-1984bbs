总参谋长陈炳德观摩南海大演练 <br />
<br />
　　7月28日，中央军委委员、总参谋长陈炳德在观摩南海舰队组织的海军多兵种合同实兵实弹演练时强调，要认真贯彻胡主席一系列重要指示和军委部署要求，着眼生成体系作战能力，积极推进军事训练转变。中央军委委员、海军司令员吴胜利陪同观摩。 <br />
<br />
[ 转自铁血社区 <a href="http://bbs.tiexue.net/" target="_blank">http://bbs.tiexue.net/</a> ]<br />
　　陈炳德指出，要紧贴部队中心任务，大力加强军队党的建设。始终把主要精力集中在谋全局、抓中心上，充分发挥党组织在完成中心任务中的领导核心作用，使军队党的建设真正融入中心任务，运用党建成果服务中心任务。大力加强学习型党组织建设，突出信息化知识学习、提高信息化素养，增强领导部队建设、指挥信息化条件下作战的实际本领。注重在学习中实践、在实践中学习，充分用好亚丁湾护航行动这一重要实践平台，虚心借鉴外军有益做法，积极开展护航合作和军事外交，在高标准完成护航任务中全面提高各级指挥员的组织指挥能力和部队遂行任务的能力。 <br />
<br />
　　陈炳德强调，要高度关注形势任务发展变化，扎实做好军事斗争准备。军事训练是军事斗争准备最大量、最基础的工作。要切实把军事训练摆在战略位置，着眼生成体系作战能力，深入推进军事训练转变，进一步兴起大抓军事训练的热潮。要深入探索信息化条件下训练规律，真正搞清海军军事训练从机械化条件向信息化条件转变的目标任务方法和途径，为军事训练转变提供科学指导。要高度重视技战术基础训练，深化全要素全系统联合训练，充分发挥指挥信息系统的“血脉”作用，提高体系作战能力。 <br />
<br />
　　陈炳德要求，要紧紧围绕提高建设质量效益，积极主动落实各项改革举措。坚持向科技进步要战斗力，以提高质量效能为核心，把战斗力新的增长点放在抓好新型作战力量建设上；按照体系要求优化力量结构，努力形成与使命任务相配套的力量体系；围绕整体配套推进建设，防止短板弱项制约基于信息系统体系作战能力的生成提高。 <br />
<br />
　　由南海舰队组织的这次演练，以实战化为背景，突出复杂电磁干扰条件下的实兵对抗，使联合作战贯穿全程，共演练了复杂电磁环境下多兵种合同对海远程精确打击、航空兵制空作战和复杂电磁环境下水面舰艇编队综合防空反导等6个课题，是海军历史上参训要素全、实射导弹多、信息化程度高、电磁环境复杂的一次演练。通过演练，实际检验了指挥信息系统是否“联得上”、“打得准”、“用得好”、“抗得真”等问题。 <br />
<br />
　　总参谋长助理戚建国，广州军区司令员徐粉林，国家和总部机关有关领导，部分国防工业部门领导等观摩了演练。（《解放军报》）</div>

							
							
							
							
