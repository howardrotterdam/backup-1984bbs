　　据《中国日报》海外版报道，2009年6月23日至24日，中国人民解放军副总参谋长马晓天和美国国防部副部长米歇尔・弗卢努瓦在八一大楼共同主持了第十次中美国防部防务磋商，双方同意于当年晚些时候在北京举行海上军事安全磋商机制专门会议，并讨论“一系列新的原则，以指导两国间的海上事务”。双方或许都希望能够取得一致，但是若忽视了各自的基本利益必将铸成大错。费舍尔建议华盛顿应当向北京施加压力使后者公开其对南中国海的战略意图，同时认真考虑同北京签署核武器限制条约的可能，迫使中国对其战略进攻和战略防御力量进行平衡，并增加中国军事力量的透明度，“这对于双方都将是最有利的结果。”另外，美国政府还应当对中国发出警告，南中国海的进一步军事化将迫使美国率领该地区的其他国家做出反应。为了避免美苏海上冷战再次上演，中国必须重新认真考虑将弹道导弹核潜艇的活动范围延伸至南中国海的基本立场并且放弃进一步扩大对南沙群岛军事控制范围的目标。 <br />
<br />
不过，费舍尔还指出，美国应当做好中国无视压力，继续扩大在南中国海军事存在的应对准备。由于1995年未能做出足够强硬的反应，如今美国可能要面对中国在美济礁修建军事基地的严酷前景，届时中国将不仅有能力从这里对美国及其盟国发动核打击，而且还对菲律宾和通过该海域的国际海上航线形成直接的威胁。这也就意味着美国决不能对现有的航母战斗群进行削减，同时应该进一步增强它们的现代化程度，并大幅增加攻击型核潜艇的部署数量。此外，华盛顿或许还有必要加强同日本、印度和澳大利亚海军的合作力度，同时寻求途径以加强与菲律宾之间的常规军事合作。 <br />
