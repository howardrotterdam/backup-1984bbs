美济礁大型“军事基地” <br />
<br />
　　张黎上将建议的第三部分，也就是在南沙群岛新建机场和码头的提议目前似乎尚未实行。费舍尔认为，中国的做法是采取渐进措施，逐步在南海中部至南部海域内修建一批军事设施。自1974年夺回西沙群岛的主权之后，北京一直等到1988年才开始了下一步军事动作，夺回了南沙群岛的6座岛礁，期间对一些由越南军队非法占领的岛屿发起了进攻。随后北京在1994年末到1995年初悄悄地夺回了美济礁。在这三个事件中，中国总是选择华盛顿或莫斯科被其他事物缠身，无暇关注南中国海动向的时候采取行动。 <br />
<br />
　　在1995年的时候，中国在美济礁上还只有一座用金属和木料搭造的高脚屋，但是到了1998年，这座平台就被礁北端新建的一座混凝土建筑物所取代了。然而2004到2007年间，解放军似乎开始逐步扩建其在南沙群岛的军事设施。2006年，在一座不知名的小岛上出现一个带有圆屋顶的新塔，从外形上看似乎是一座雷达设施。2007年11月，简氏信息集团获得了一张卫星照片，照片显示美济礁上的军事设施得到了扩建，在礁的南端又出现了一座规模更大的混凝土建筑。这张照片还显示出了一些其他的内容，包括一艘海洋测绘船和若干艘其他舰船，这些船可能为美济礁的扩建活动提供了支持。 <br />
<br />
　　根据张黎上将的方案，要想在美济礁修建简易跑道和码头，解放军就必须付出更多的努力。包括大量的石料和水泥，这些都是修建跑道、码头、仓库和人员居住设施所必需的。美济礁基地最终会达到什么规模尚不得而知，不过许多中国军事爱好者都提出了自己的建议。如果解放军在美济礁修建海空基地的话，那么为了满足J-10和J-11多用途战斗机的起降需要，飞行跑道的长度至少要达到1000米左右。 <br />
<br />
[ 转自铁血社区 <a href="http://bbs.tiexue.net/" target="_blank">http://bbs.tiexue.net/</a> ]<br />
　　不过J-10在配备了矢量推力发动机后将具备短距起降的能力，而最终解放军可能会研制出一种专用的短距/垂直起降战斗机，这将大大降低对机场跑道长度的要求。如果跑道长度达到2000米，那么美济礁基地将能够起降中型运输机或更大一些的海上巡逻机。码头方面，美济礁目前的周边水深只能满足解放军小型战舰、护卫舰和扫雷艇的驻泊需要，要想停靠更大型的战舰，就必须大规模地开挖航道，建造新码头。在新建码头或跑道的同时，解放军还可能会兴建一批基础设施，以便未来部署岸舰导弹、现代化防空系统、巡航导弹或新型远程反舰弹道导弹之用。另外，如果美济礁周边水域能够被挖深，那么解放军现役或未来即将装备的防空驱逐舰完全可以停靠在这里，这些战舰也能够发射远程防空和反舰导弹。 <br />
<br />
　　费舍尔根据上述所谓“理由”推断，中国事实上正在按照张黎上将的建议加大对南海海域的军事投入力度，以便未来进一步控制尚未掌握的南海岛礁。那么在美济礁建造大型海空基地能够给解放军的行动提供哪些帮助呢？首先，配合西沙群岛北部永兴岛上现有的空军基地，美济礁基地将成为解放军在南海海域的一个重要的前进据点，以此为核心，解放军的武力打击范围将延伸至整个南中国海海域。其次，在这里还可以设置雷达或水下声纳阵列管理中心，指挥解放军作战部队对不请自到的美国或其他亚洲国家军事力量进行驱逐。美济礁海空基地还有助于解放军海空力量监视从马六甲海峡至此的国际海上航线。此外，利用该基地，解放军能够更迅速方便地对可能出手援助菲律宾的美国军队发动打击，或者使用小型布雷舰和高速攻击艇截断菲律宾的海上贸易航线。 <br />
