　编者按：有媒体认为，美国国务卿希拉里・克林顿近日针对南中国海问题的挑衅性发言，也许预示着美国在该地区将采取更广泛的卷入政策，这也很可能为该地区本就复杂的安全形势增添新的重要变量。对此，解放军新一轮南海演练体现出其在可能解决相关问题上的军事准备成果，而理查德・费舍尔的这篇文章似乎也在对中国媒体报道的捕风捉影中有了重大发现――“中国将在南海美济礁建设大型军事基地。”费舍尔是西方最有代表性的“中国威胁论”鼓吹者之一，本刊刊发他的这篇文章绝不代表同意其任何观点，更非证实其中任何内容，仅供有兴趣的读者及专业工作者参考。 <br />
