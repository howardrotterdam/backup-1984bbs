马来西亚媒体批 <br />
<br />
美国挑拨中国-东盟关系 <br />
<br />
　　美国国务卿希拉里上周发表政策性言论，宣称南海主权争议是美国国家利益的一部分，各国不应通过强迫或威胁途径来解决南海争议。该国希（拉里）、斯（坦伯格）两人高调发表针对南海问题之论，显然是冲着中国而来，不愿中方单独处理有关争议，并企图通过介入该争端来深化美国在东盟的影响力。尽管如此，一般东南亚国家领袖迄今对有关言论冷静待之，甚至对美国发出吹绉一池春水，干卿何事之疑虑。 <br />
<br />
　　希拉里选择在东盟区域论坛上提起南海问题，是在挑动中国敏感神经，挑拨中国和东盟诸国的友好关系。它亦反映美国的隐藏议程，即试图借南海争端来维护和提升在此海域的军事利益，开创美国和其南海域外盟友在此区域的新战略布局，以对中国进行新的钳制围堵。 <br />
<br />
　　美国近来假借天安舰事件在东北亚耀武扬威，甚至一度高调调动航空母舰参与美韩在黄海的军事演习，惟在中国强烈反对下仅变成东海（指日本海）军演。美国未因此收敛其霸气，反而把目标转移到南海，图借主权争端制造中国和东盟诸国间的对立假象，让东南亚国家掉入她企图另辟中美博弈场所的阴谋诡计。 <br />
<br />
[ 转自铁血社区 <a href="http://bbs.tiexue.net/" target="_blank">http://bbs.tiexue.net/</a> ]<br />
　　对中国来说，美国此举极具挑衅动机，其实，美方倘若继续搅局，只能在南海营造无谓的紧张氛围，无助于解决争端。中国官方媒体在题为《“南海问题多边解决”是伪命题》的评论文章中清楚表明，中国只与部分东盟成员国存在南海问题，而不是整个东盟之间的问题。 <br />
<br />
　　文章抨击美国企图挟东盟以“对付”中国的作法实际上是将“一己之私”强加于所有东盟成员国身上，它并指责美国和日本等南海域外大国企图利用南海主权问题来鼓噪“中国威胁论”，甚至利用它来胁迫南海周边国家来为自己的利益服务。 <br />
<br />
　　东盟6个老成员国和中国在今年初落实自由贸易区协议，中国已经成为东盟的第四大贸易伙伴国，双边经济合作、优势互补，友好关系不断拓展与提升。 <br />
<br />
南海诸岛问题错综复杂，争议悬而未决，为不影响区域合作大局，各国宜坚守共同签署的《2002南海各方行为宣言》，通过理性协商和互信，坚持不付诸武力或强硬行动来处理主权争议，确保本区域的稳定与和平发展，以免美国等域外强国借机介入南海主权争端，使问题扩大及复杂化。（《南洋商报》） <br />
