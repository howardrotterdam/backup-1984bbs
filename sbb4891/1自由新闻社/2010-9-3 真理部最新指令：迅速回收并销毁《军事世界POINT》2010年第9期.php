　　南海问题是否会成为日前在越南河内举行的东盟地区论坛外长会议的突出议题，是中国代表团重点关注的一个问题。因为美国等国家在会前和会议期间的一系列动向使中方有了这种预料。果然，美方没有听中方的劝告，在会上发难了。为了维护中国主权和合法权益，推动南海问题在声索国之间通过双边谈判妥善解决，维护南海的稳定，杨洁篪外长作了发言。会后10多位亚洲国家代表向中方表示祝贺，他们表示，杨外长的讲话长了亚洲人的志气，为此感到骄傲。以下为杨外长的发言内容： <br />
<br />
[ 转自铁血社区 <a href="http://bbs.tiexue.net/" target="_blank">http://bbs.tiexue.net/</a> ]<br />
　　“首先，要看看南海的形势到底是和平、稳定的，还是紧张的？在今天的讨论中，多数人说形势是和平的，在我同东盟国家以及其他国家的双边会谈中，大家都说现在没有任何威胁地区和平稳定的事态。 <br />
<br />
　　第二，南海问题是中国同东盟之间的问题吗？答案是否定的。中国和东盟一些国家有领土和海洋权益争议，是因为中国和他们是邻国。不能因为这些国家是东盟成员，就要说成是中国与东盟的争议。东盟中的非声索国对中方说，他们不是争议方，不愿站队，希望通过双边协商解决。 <br />
<br />
　　第三，本地区国家在南海问题上的共识是什么？共识就是通过友好协商和平解决争议，以维护南海和平稳定，维护睦邻友好。《南海各方行为宣言》的精神就是要保持克制，不将南海问题国际化、多边化。不能罔顾一个事实，就是目前当事国讨论这个问题的磋商渠道已经存在，而且是畅通的。 <br />
<br />
　　第四，《南海各方行为宣言》的作用是什么？它的作用就是增进有关国家之间的互信，为最终解决争议创造有利条件和良好气氛。中国和东盟国家发表了《南海各方行为宣言》并已经有了联合工作组会议磋商，条件成熟的时候也可以举行高官会。 <br />
<br />
　　第五，南海地区国际航行自由和安全出问题了吗？显然没有。这个地区的国际贸易发展得很快，中国已成为本地区许多国家最大的贸易伙伴国。一些国家不能增加对华出口，不是因为航行自由受到阻碍，而是因为对高技术产品输出设置了高壁垒。 <br />
<br />
　　第六，在南海问题上谈“胁迫”用意何在？中国一贯主张国家不分大小一律平等。中国作为大国也有自己的合理关切，难道表达合理关切就是胁迫吗？这是说不通的。倒是南海非声索国对有人胁迫他们在南海问题上站队感到反感。 <br />
<br />
[ 转自铁血社区 <a href="http://bbs.tiexue.net/" target="_blank">http://bbs.tiexue.net/</a> ]<br />
第七，将这个问题国际化、多边化会有什么后果？这只能使事情更糟，解决难度更大。国际实践表明，这类争议的最佳解决途径是争端当事国之间的直接双边谈判。亚洲在崛起，有了自己的尊严，亚洲国家能够平等相待，相互尊重，解决好彼此的关切。”（外交部网站） <br />
