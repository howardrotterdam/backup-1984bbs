南海已成为“战略海域” <br />
<br />
　　南海海域的海上航线对于亚洲各国的经济而言至关重要，目前有六个南海沿岸国家宣布拥有并实际占领了数量不等的西沙和南沙群岛岛屿。中国政府一方面不断强调南中国海的绝大部分海域均为中国领海，另一方面在缺少地区冲突解决框架的情况下多次提出“搁置争议，共同开发”的主张，希望能够加强各国间的合作，缓解南海地区的紧张态势。此外，中国政府一直宣称依据《联合国海洋法公约》，中国有权力阻止外国军事力量在其专属经济区内进行军事活动，但华盛顿却并不承认这一规定。 <br />
<br />
　　从上世纪70年代中期开始，北京逐步展开了一系列的军事行动（包括同越南和菲律宾之间爆发的海上冲突），逐步夺取了南海部分岛屿的控制权，并以军事占领和法律认定的形式加强了对南沙诸岛的控制力度。费舍尔认为，中国政府这样做的原因随着本世纪初在海南岛三亚附近开工新建大型海军基地的行动而逐渐变得明朗起来。这座基地将成为中国海军第二代弹道导弹核潜艇的主要驻泊地之一，中国政府很显然希望在海南岛附近海域构筑起一片坚固的“堡垒”阵地，以拱卫新海军基地和导弹发射海域的安全，确保“二次打击”核威慑力量的可靠性。分析人士根据公开的信息推断，中国最终可能会把大约一半的核导弹都部署到战略核潜艇上。这在一定程度上解释了中国政府为何会对美国海军监视中国军事力量在南中国海活动的做法越来越反感。 <br />
<br />
　　费舍尔宣称，中国既没有公开正式宣布其未来向该地区部署战略核力量的计划，也没有就为何要在南中国海地区加强军事存在的原因同本地区内的其他国家进行磋商，而这些磋商或许能够在一定程度上增加解放军的透明度，并减少该地区的紧张状态。2009年7月，北京和华盛顿围绕着“海上事故”这一议题进行了讨论，但是此前中国已经拒绝签署正式的“海上事故”协议（目前签署该协议的有美国、英国和俄罗斯），而似乎屈从于国内的压力下定决心加强军事实力，以便为今后“更加大胆”的行动打好武力基础。 <br />
<br />
　　2009年，中国媒体发布了一条可能具有重要意义的消息：当年初因年龄原因退役的解放军原副总参谋长张黎上将，呼吁在存在争议的美济礁上修建正式的海空基地。这座海礁位于菲律宾巴拉望岛以西150英里的海域，距离中国大陆南部沿海约800英里，1995年，中国军队收复了这座岛礁。迄今为止，中国仅仅在这座岛礁上修建了两座小型建筑。如果大型海空基地修建完成，将能够部署大量的海、空和导弹力量，从而扼守住巴拉望海峡――亚洲最为重要的海上航道之一。费舍尔认为这不仅仅对菲律宾而言是一个新的军事威胁，更重要的是掐住了对海上贸易极度依赖的美国盟国们（如日本和韩国）的咽喉，使中国在同它们打交道时“更有底气”。 <br />
