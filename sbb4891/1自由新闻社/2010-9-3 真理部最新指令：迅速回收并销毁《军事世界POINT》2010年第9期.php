													<h2>解读美国2010版《中国军力报告》</h2>
						
						
																											<div id="postmessage_811720" class="t_msgfont">以保持压力达成优势对话<br />
<br />
解读美国2010版《中国军力报告》<br />
<br />
（《军事世界》月刊2010年9月号主编论点）<br />
<img src="http://static16.photo.sina.com.cn/middle/3c7480d3h8e2a1cbfcd4f&amp;690" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
这是一个急遽变化的世界，美国今日的一切重要国家利益均依赖于美国在二战后建立和在冷战后膨胀的优势军事地位。而对这一优势地位的威胁可能来自不友好的意图，但更可能来自不友好的政策意图指导下的突破性技术能力的发展。<br />
<br />
 <br />
<br />
经过推迟，美国国防部的年度《与中国相关的军事与安全发展报告》（Militaryand Security Developments Involving the People’s Republic of China,简称《中国军力报告》）终于提交国会。细读本年度报告，总体来说，并无特别新意，但联系过去30年以来中美军事安全关系的演变和今日两国的在全球格局中的基本现实，我们仍可以透过该报告对美国战略决策部门对中美军事关系及对华军事安全思维有相当进一步的认知。<br />
<br />
一般认为，美国冷战后因应中美军事安全关系的最主要现实是中国作为主要经济大国的崛起，经济的发展和力量不仅改变全球经济版图，也给军事格局和安全发展带来新的前景，这些本已模糊的前景再加上对话机制的不完善、意图的猜忌和技术能力的发展，无疑对于双方来说，都构成了提防不乐观未来的足够理由。特别是，考虑到中美在全球地缘战略格局中的地位、两国历史文化及政治制度的差异，以及历史和现实的冲突，中美之间存在不小的军事安全忧虑，这是任何欲认识两国关系的人不可回避的事情。正是从这一基本的现实认知出发，我们可以了解无论两国关系取得何种良性进展，而美国总有相当一部分人厉声喧嚣各种版本的“中国威胁论”，而与此同时，在中国也不时可以听到“美帝忘我之心不死|”的警世恒言。<br />
<br />
本年度报告引起中国社会和媒体格外关注的另一重原因是：近来美军在中国周边展开了频繁的动作，这些动作既包含实战的意义，也牵涉复杂的外交动态，这些行为与金融危机爆发后中美在经济领域加强合作的大势相悖，同时也与美国政府领袖们敦促恢复和加强两国军事交流的吁请相冲突。这一矛盾现象给国内公众认识中美军事安全关系带来了实在的困扰。这是阅读和品评本年度《中国军力报告》的近景。然而，必须看到，作为全球经济分量最大的两大国，中美军事与安全关系不仅牵涉中国的未来，也在很大程度上将决定美国的未来，尽管困难重重，但两国在军事安全领域的合作和对话的迫切必要性是真实的，其对两国现实和未来的意义也是不言而喻的。因此，从这个现实和迫切的必要性出发，我们会很容易看到美国包括本年度报告在内的一系列动作背后的基本意图，那就是：借压力和对话的交替两手，保持和巩固军事安全优势地位，借压力而实现对自己更有利的对话，及我所称的优势对话。这是为什么本年度报告中一方面赞扬中国增强军事能力后的某些行为和政策，而依然重申和强化在所谓“透明化”等问题上的要求。<br />
<br />
熟悉军事历史的人们不难知道，若就实践的层面来说，在两大国之间实现意图和能力的双重、彻底透明，这本是不可能之事。一则，贯串政策意图的需求评估和环境评估是动态变化的，再则，随着某国对军事安全目标的界定，能力的需求和建构也是非对称的进程。美国的战略思想精英们并非不明此理，相反，<font color="red">正是因为他们深知，就现实和可预见的将来而言，中国对美国的军事安全基本态势并无任何威胁</font>，推动朝野上下保持对中国军事长期热情的，与其说是对现实的认知，不如说是对未来的焦虑。这是一个急遽变化的世界，美国今日的一切重要国家利益均依赖于美国在二战后建立和在冷战后膨胀的优势军事地位。而对这一优势地位的威胁可能来自不友好的意图，但更可能来自不友好的政策意图指导下的突破性技术能力的发展。诚如中国古人所说，“君子无罪，怀璧其罪，”单以中国过去30年来取得的经济成就而言，要美国不提防和怀疑中国的未来军事安全意图，那几乎是缘木求鱼，这是任何透明化的对话都无法达成的目的。<br />
<br />
反过来说，因为有如此与中国有关而又经常不便明言的未来军事安全焦虑，所以也可以说，美国呼吁和推动中美军事交流、对华的热情是基本真诚的，他们认为这样的交流有助于缓解和理解上述对不确定未来的焦虑，有助于在己方处于现实优势地位时塑造和建构更有利的未来。理解这一层，对于我们阅读本年度的《中国军力报告》和认识近期的中美军事互动动态会有十分重要的帮助。<br />
