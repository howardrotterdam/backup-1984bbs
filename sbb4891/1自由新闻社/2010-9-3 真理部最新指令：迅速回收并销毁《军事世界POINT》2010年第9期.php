避免海上新冷战 <br />
<br />
　　2009年6月11日，一艘中国海军潜艇在距离菲律宾苏比克湾144英里海域，与美国海军“麦凯恩”号导弹驱逐舰所布放的拖曳声纳阵列相撞。而就在不久之前的3月8日，数艘中国海事船在距离海南岛约75英里的海域跟踪并驱逐了美国海军正在执行测绘任务的“无暇”号海事测量船。这些事件说明随着中国在南中国海海域军事存在的不断增强，中国同美国和其他亚洲国家之间的关系正在变得越来越微妙。 <br />
<br />
　　虽然美国不可能对全世界的每一个角落都照顾得到，但费舍尔还是宣称――南中国海地区对于美国的利益是至关重要，不容忽视的。事实上，南中国海的海上航线承担着日本每年石油进口量的80%以上，对于美国这个亚洲地区最重要的盟友而言，南海航道就是它的生命线。显然中国打算将南中国海打造成它的一个坚固的“海上堡垒”，以保障未来弹道导弹核潜艇的作战安全，由于这些核潜艇上所搭载的弹道导弹可能主要瞄准了美国，因此南海海域直接关系到美国本土的安全。不过美国政府对于南海主权争端的传统立场是保持中立。1995年中国军队进驻美济礁后美国政府又再次重申了这一立场，不过当时的克林顿政府曾解释说如果中菲两国开战的话，美国政府将会履行对菲律宾盟友的安全承诺。 <br />
<br />
　　费舍尔不无“遗憾”地写道，1995年的时候，华盛顿没有做出严厉的反应，那么现在它就要面对中国可能要在距离菲律宾沿海仅仅150英里的地方扩建海空基地的严峻现实。仅仅依靠外交压力就想迫使中国放弃扩建美济礁基地甚至撤销这个哨所似乎可能性不大。北京曾拒绝了美国提出的签署“海上事故”处理协议的建议，相反中国一直坚持美国海军的战舰和潜艇不应当侵入其领海，随着《联合国海洋法公约》的颁布，中国政府的这一立场愈发强硬。如果中国成功地在南沙群岛扩建军事设施，并组建新的海空部队来保卫它们的话，今后它同华盛顿谈判的兴趣以及对于宣称拥有南海岛礁主权的其他亚太邻国的忍耐性都将进一步下降。在这种情况下，费舍尔认为有理由预计美中两国海军间的“海上事故”将会越来越多，有朝一日中国大陆甚至有可能会对越南、菲律宾所占据的南沙岛礁发动打击，夺回对它们的主权。 <br />
<br />
