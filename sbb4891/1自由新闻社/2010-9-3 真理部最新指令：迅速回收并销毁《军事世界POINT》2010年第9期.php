全面监控南海 <br />
<br />
　　实际上近些年来，张黎上将建议中的部分内容早已开始实施。中国一直在不断扩充其海事监察部门的实力，相继为其配备了多艘排水量更大的舰船，以提高海事监察部门的巡逻范围，更灵活有力地维护国家主权。2003年，国家海事局接收了第一艘排水量达到1500吨的“海巡-21”号巡逻舰。虽然这艘巡逻舰的武器装备远逊于海军装备的护卫舰，但是却搭载有一架直升机。最近发布在某中文网站上的一篇报道称中国可能正在计划建造数量更多的排水量1000、1500和4000吨级的海事巡逻舰，用于执行此类使命。 <br />
<br />
[ 转自铁血社区 <a href="http://bbs.tiexue.net/" target="_blank">http://bbs.tiexue.net/</a> ]<br />
　　如果事实果真如此的话，那么中国海岸警备部队所装备的舰艇吨位将不逊色于日本海岸警卫队。2009年6月中旬，中国大洋矿产资源研究开发协会开始在南海海域对排水量5600吨的“大洋一号”科考船进行海试，该科考船于1994年从乌克兰购入，2002年进行了现代化改装。这或许是中国最现代化的大洋海床调查和测绘船了，上世纪90年代中期还曾与中俄联合研制的CR-01型深海无人驾驶水下航行器合作执行过大洋测量任务。 <br />
<br />
　　张黎上将呼吁装备更多的海事巡逻飞机，前文提到的关于组建海岸警卫力量的报道也昭示出了这一需求。目前中国海事监察部门和海军航空部队只装备了少量的小型Y-12双发涡桨运输机和中型Y-8四发涡桨运输机改装的巡逻机来执行此类使命。这些巡逻机无论是装备数量还是航程均无法满足远巡南中国海的需要。 <br />
<br />
　　不久前曾有消息称中国有兴趣购买或同俄罗斯合作生产现代化的别-200型双发涡扇两栖飞机。不过，最新的消息则指出中国已经决定自行研制“蛟龙600”（JL-600）大型四发涡桨式水陆两用飞机，该机的概念图在2008年11月举行的珠海国际航展上首次亮相，原型机预计最早将于2012年试飞。虽然该机的详细性能参数还不得而知，但是陕西Y-9新型运输机所使用的大推力涡桨发动机预计将是一个可能的选择。JL-600的载重量有可能将达到15-20吨，接近洛克希德-马丁公司C-130的标准。由于解放军在南沙群岛驻守的岛礁周围大多水深较浅，因此解放军可以利用JL-600迅速高效地向南沙群岛的前哨基地运送后勤补给和兵员。随着中国部署在南沙群岛的基地规模和部队数量不断增加，对于后勤保障的需求也将必然加大。JL-600还可以执行远洋巡逻、反潜、反舰和布雷等使命。 <br />
<br />
　　张黎上将提出的建立“立体侦察观测系统”以实现“综合完整的”信息情报搜集的建议也正在逐步变成现实。解放军正在发展超视距雷达、海底声纳系统和海事监察卫星，这些新型装备可以被整合成一个综合性的战区信息网络，覆盖未来中国弹道导弹核潜艇在南中国海地区的所有活动海域。 <br />
<br />
　　费舍尔宣称，2008年有消息灵通人士告诉他――中国已经在海南岛新建了一座大型远程超视距雷达站。随后在2009年二月举行的阿布扎比国际防务博览会上，俄罗斯方面的消息人士向费舍尔又“证实”了俄曾向中国出口探测距离300公里的“Podsolnukh-E”型表面波超视距雷达的传闻。俄罗斯还向国际市场推出过一款探测距离达3000公里的天波超视距雷达系统，不过目前尚未有解放军购买该雷达的报道。实际上，中国早在多年前便已经开始了国产天波超视距雷达的研制工作。 <br />
<br />
　　费舍尔还自称在2008年初，有消息人士告知他中国正在铺设水下声纳阵列，但尚无证据能够证明这些系统是否来自俄罗斯。水下声纳阵列对于潜艇的发现和跟踪尤其有效，如果在一些较大的岛礁如永兴岛或者美济礁上设置声纳阵列管理中心的话，任何闯入解放军弹道导弹核潜艇发射阵地的敌方潜艇都将无所遁形。 <br />
<br />
