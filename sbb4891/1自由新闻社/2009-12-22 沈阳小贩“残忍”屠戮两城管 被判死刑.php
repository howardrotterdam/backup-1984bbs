<br />
事后，夏俊峰逃离案发现场，但没有跑多远就被抓获。检察机关以其构成故意杀人罪对其提起公诉。<br />
<br />
法院认为，被告人夏俊峰在与城管执法人员因摆摊占道问题发生争执后，持刀在被害人办公室行凶，造成2人死亡、1人受重伤。其犯罪手段特别残忍，情节极其恶劣，后果极其严重，应依法严惩。遂作出上述判决。<br />
