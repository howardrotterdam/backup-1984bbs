<br />
&nbsp; &nbsp;&nbsp; &nbsp; 由旅法华人社团联合发起的法国华侨华人促进改善社会治安“反暴力、要安全”大游行于20日下午从巴黎美丽城出发，预计将有上万名旅法华侨华人参加。据悉，这或许是法国华人历史上首次因社会问题引发的最大规模的一次游行。对此，除了法国主流媒体给予极大关注外，当地政府对这次游行也表示支持。<br />
<br />
<br />
　　游行起因于6月1日深夜，在法国巴黎美丽城街区，一群不法歹徒围堵在中餐馆外，肆无忌惮地对参加完婚宴陆续离开的华人轮番抢劫，一位华人忍无可忍拔枪解围，击伤一名歹徒。<br />
<br />
<br />
　　据此事件目击者说，当晚婚宴结束，宾客开始陆续离去时，多位宾客遭到围堵在酒楼门外的一群歹徒的抢劫，歹徒们的嚣张气焰令恐慌的宾客们感到异常愤怒，很多人都躲在一楼出口处不敢出门，这时一名宾客走出大门，拔出随身携带的一把手枪，在向天空连发数枪示警后，再连发两弹分别击中其中一名歹徒的臀部和腿部，其他歹徒见状立刻仓皇逃走。目击者称，这位枪击歹徒的华人当晚被随即赶来的警察带走。<br />
<br />
<br />
　　据了解，虽经华侨多方奔走，当日拔枪解围的华人钟少武目前仍被当地警方关押。<br />
<br />
<br />
　　美丽城事件发生后，面对近来针对华裔群体犯罪增多的形势，旅法侨团积极筹备联手走上巴黎街头，为呼吁改善治安状况进行游行。<br />
<br />
<br />
　　希拉克养女声援<br />
<br />
<br />
　　经巴黎警局正式批准，由旅法华人社团联合发起的法国华侨华人促进改善社会治安“反暴力、要安全”大游行于20日下午2点在美丽城集合，3时正式出发，一直进行到下午5时结束。目前已有30多家华人社团参与本次游行活动，游行队伍中既有华人社团代表，也有旅法各界人士和留学生，预计总人数将达到1万人。<br />
<br />
<br />
　　据法国国际广播电台报道，法国前总统希拉克所收养的越南裔女儿也表示会参加游行，巴黎第20区区长也已确定在游行开始前到场声援。<br />
<br />
<br />
　　游行组委会负责人、法国华侨华人会主席陈胜武说，近年来，巴黎一些地区治安状况不佳，当地居民深受其害，尤其是当地华人和中国赴法游客屡屡成为盗抢目标。华人聚居的美丽城地区位处多个行政区域交界处，人员复杂，几乎每天都有抢劫或盗窃事件发生，严重影响了华侨华人的正常生活。(宗禾)</div>

							
							
							
							
