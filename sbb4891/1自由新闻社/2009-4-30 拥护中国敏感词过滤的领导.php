　　（5）已获硕士学位人员提供硕士学位证书复印件（在国外获得的硕士学位须提供教育部留学服务中心的认证书）、毕业证书复印件（专业学位研究生和以同等学力申请硕士学位人员不必提供）、硕士学位论文，及硕士课程成绩单原件（由考生攻读硕士学位所在院校研究生培养部门提供并加盖公章，也可从本人档案管理部门复印并加盖其公章。下同）； <br />
　　应届硕士毕业生只提交硕士课程成绩单原件。被录取后，如在入学报到时不能获得硕士学位证书及毕业证书将被取消入学资格。 <br />
