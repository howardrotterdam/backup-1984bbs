欢迎报考北京师范大学研究生！<br />
　　北京师范大学是一所教学与科研力量居全国一流水平的著名重点大学，有逾百年办学历史。1984年经国务院批准首批建立研究生院。目前学校已成为我国培养高层次专门人才和进行多学科、高水平科学研究的重要基地。学校在“七五”、“八五”期间，被确定为国家首批重点建设的十所大学之一。“九五”、“十五”期间，又被首批列入“ 211 工程”建设计划。在教育部“教育振兴行动计划”中，作为有特色的重点大学得到了国家和北京市的支持。1999年列入国家“ 985 工程”重点建设的38所大学之一。我校学科门类齐全，共有16个博士学位授权一级学科、19个硕士学位授权一级学科。95个博士学位授权学科专业，157个硕士学位授权学科专业，另有包括教育硕士、工商管理硕士（MBA）、公共管理硕士（MPA）和法律硕士等在内的8个专业学位授权学科(北京师范大学2009年博（硕）士学位和专业硕士学位研究生招生专业一览表)。有5个国家级重点一级学科，11个国家级重点二级学科。有2个北京市重点一级学科，9个北京市重点二级学科。在18个学科设有博士后流动站。有7个国家文理科基础学科人才培养和科学研究基地，以及国家生命科学与技术人才培养基地、国家对外汉语教学人才培养基地和国家大学生文化素质教育基地各1个，2个国家级人才培养模式创新实验区。4个国家重点实验室，7个教育部重点实验室实验室和5个北京市重点实验室，4个教育部工程研究中心和2个北京市工程技术研究中心。7个教育部人文社会科学重点研究基地和1个北京市哲学社会科学重点研究基地(各重点学科、实验室、研究基地和培养基地介绍)。学校现有博士生导师760余人，硕士生导师1400余人，导师中有两院院士19名，还有一批蜚声中外的资深教授。将北京师范大学建设成为综合性、有特色、研究型的世界知名高水平大学是学校的发展目标。<br />
