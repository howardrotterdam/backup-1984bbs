　　初试科目：外国语及两门业务课。外国语含听力，分值约占总分的15%。 <br />
　　初试时间：暂定2009年3月21日、22日。我校可能会调整初试时间，如调整，将最迟在2009年2月底前在研究生院网站公布，届时请注意查看。 <br />
　　初试地点：北京师范大学（具体地点以考试证为准） <br />
　　同等学力人员须加试两门硕士阶段专业基础课和政治理论。 <br />
　　复试有可能安排在初试后马上进行，也可能在初试成绩公布后进行，具体安排请于考试前一周在研究生院网站查询。 <br />
　　五、录取　　<br />
　　根据初试和复试成绩，综合考虑考生的科研情况和工作业绩等，德智体全面衡量，择优录取。<br />
<br />
六、关于培养机制改革　　 <br />
　　为提高博士研究生培养质量，我校自2009年起实行研究生培养机制改革。实行培养机制改革后，博士生录取类别只分为非定向培养博士生和定向培养博士生两类。人事档案和户口都转入北师大的为非定向培养博士生，不转入北师大的为定向培养博士生。 <br />
　　北京地区考生在单位保证其学习时间的前提下可在职攻读博士学位，其他博士生均须全脱产学习3年。 <br />
　　我校设立“研究生基本奖助学金”，用于资助非定向培养博士生的全部或部分学费和生活费。全体非定向培养博士生均可获得基本奖助学金。基本奖助学金实行动态评定，每位非定向培养博士生获得的奖助学金等级和额度由各院系所根据其学习成绩、科研能力等综合确定。一般而言，一等奖助学金的额度将达到20000-22000元/年，即免除当年学费10000-12000元外，还可获得10000元/年的生活费。各院系所还可以视经费筹措情况调高此额度。 <br />
　　定向培养博士生、已获博士学位人员及年龄超过45周岁人员不享受 “研究生基本奖助学金”，且须缴纳学费，学费标准为三年共计3-3.6万元。<br />
