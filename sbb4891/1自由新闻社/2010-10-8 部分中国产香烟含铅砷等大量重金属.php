美国和加拿大研究人员发现，中国大陆生产的部分香烟中含有大量铅、砷和镉等重金属成分，其含量与加拿大产香烟相比，最高超出三倍以上。据路透社报道，上述重金属成分通过食品摄入人体有害健康，这已经是常识。但对于它们通过呼吸进入人体会产生何种健康後果，目前几乎毫无研究。<br />
