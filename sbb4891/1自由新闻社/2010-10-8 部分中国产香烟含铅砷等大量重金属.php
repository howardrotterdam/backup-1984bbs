上周四，学术期刊《烟草控制》发表了上述美国和加拿大科学家的研究论文。论文第一作者、纽约州罗斯维尔&#12539;帕克癌症研究所的理查&#12539;奥康纳写道 : “我们测量了每根香烟中的重金属含量，但真正的问题是长期吸烟的後果。因为烟草具有成瘾性，烟民们每天可能吸入多达20支香烟，持续多年。”“这些重金属与多种其他有毒成份一道被烟民吸入，而接触多种有毒成份的长期累积後果，是个公共健康话题，需要仔细研究。”<br />
<br />
研究人员选用加拿大香烟与中国香烟对比，是因为加拿大法律规定烟草生产商和进口商必须检测烟草中的金属含量，而加拿大卫生部近期披露了相关数据。<br />
<br />
中国烟民总量3.2亿，是全球吸烟人数最多的国家。中国也是全球第一大烟草生产国，2007年共生产2.16万亿支香烟。每年近100万中国人死于与吸烟相关的疾病，包括高血压、中风、糖尿病、癌症、心脏病和呼吸系统疾病等。<br />
<br />
奥康纳及其同事们检测了78个中国产香烟品牌，发现其中部分重金属含量严重高于加拿大香烟。研究团队成员、加拿大滑铁卢大学的杰弗里&#12539;方说，中国香烟重金属含量高是由于种植烟草的土壤遭到污染。</div>

							
							
							
							
