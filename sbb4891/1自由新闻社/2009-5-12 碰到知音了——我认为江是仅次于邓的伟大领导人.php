&nbsp; &nbsp; 我不是诗人和小说家，我是写剧本的。剧本要演出才能展示。现在我只能展示剧名。香港著名的香港话剧团、中英剧团以及一些业余剧团演过我五个剧本：《假如我是真的》、《寻找男子汉》、《耶稣・孔子・披头士列侬》、《幸遇先生蔡》、《约会》。我由衷地感谢香港戏剧界朋友和观众对我的厚爱，给我这么多机会展示我的剧本。此外，15年前，香港嘉禾影业公司投资拍摄我的电影剧本《江青和她的丈夫们》，都已经开始前期工作了，但给有关方面知道了，就立即请嘉禾老板饮茶聊天，劝其撤资，釜底抽薪，终于使得该片胎死腹中，这充分展示了他们在香港回归之前就已经非常强大了的执政能力。他们展示的是权力。权力剥夺我的机会，香港同行和观众却给我机会。以后如果还有机会，我仍然希望能在香港展示我的戏，敬请诸位赏光，谢谢，谢谢！而今天在这里我只能做我并不愿意但也不得不做的演讲，展示的只是嘴皮子。 <br />
&nbsp; &nbsp; 我并不喜欢演讲，因为我担心这类演讲讲多了，会让人感觉我成了文艺评论家或者政治批评家。这不是我希望的。我喜欢舞台，我不习惯演讲台。我今天没讲稿，也不习惯写讲稿。刚才有记者向我要讲稿，我说没有，我只有腹稿，在肚子里。她说，那以后要发表怎么办？我说好办，我回到上海破腹产（笑声）。 <br />
&nbsp; &nbsp; 讲什么呢？讲讲学习温家宝总理的讲话。 <br />
&nbsp; &nbsp; 有没有搞错！跑到香港来学习温家宝总理的讲话？台下已经有人对我皱眉头了（笑声）！是的，一个美国作家绝不可能做我这样的演讲，不可能学习总统布什同志的讲话；一个英国作家也不可能学习首相布莱尔同志的指示，也不可能做我这样的演讲。他们只习惯于嘲讽或者抨击他们政府领导人，否则一定会被看作拍马，视为献媚，被人认为是御用文人。<br />
&nbsp; &nbsp; 中国不一样，中国现在还是后集权国家，或者说得好听一点，还是前民主国家。西方国家一切按法律行事，管你是总统还是首相！中国不行呀，中华人民共和国建国快六十年了，快一个甲子了，至今还没新闻法，还没有出版法，无法可依呀，领导人的讲话就是法，共产党的政策就是令。以前不是常说，听毛主席话，跟共产党走吗？过年还把这两句话作为春联贴在大门上。不听行吗？不学行吗？<br />
&nbsp; &nbsp; 以前我当然也听毛主席话，跟共产党走。只要比我高的领导，我都听都跟；级别越高，我越听越跟；唯恐听得不够，跟得不紧，诚惶之至，诚恐之至。年轻的时候谈女朋友，凡是个子比我高一点的，我都不敢交往，我有“恐高症”（笑声）！<br />
&nbsp; &nbsp; 现在不一样了，脑袋在自己的脖子上了，不盲从了。对越是高的领导，我反而越不盲从。不论你多高的领导，你说的对，我就听；说的不对，就不听。比如在上一次的全国作家代表大会上，也有一个非常高的领导发表讲话，我就不听。为什么？首先他的讲话一定是秘书起草的，绝对的教条，绝对的乏味；不是真诚平等的谈心，而是居高临下的指示。他让秘书起草的那些话我也会说，说的可能比他还精彩（笑声），我听他干吗？奇怪的是，一些全国知名的作家听了这一个模子里刻出来的讲话，竟然还纷纷发表心得体会，说什么学习了这位领导人的讲话，胸中流过一股暖流（笑声）。是吗？我也摸了我的胸呀，怎么没暖流呀（笑声）？我的胸部很健康呀，怎么没感觉呀（笑声）？还有的作家说这位领导人的讲话是里程碑。我就更奇怪了，为什么领导人讲话都是里程碑呢？都是里程碑，那不成了西安的碑林了吗（笑声）？其实呀，里程碑是很少很少的，最多最多的是墓碑（笑声、掌声）！<br />
&nbsp; &nbsp; 在去年11月举行的全国作家代表大会上，温总理也讲话了。我也没去听，不是不愿意听，而是我不是会议代表，没被上海作协选上，去了说不定给抓起来。不是开玩笑呀，上次“作代会”进入会场就有好几道安检，会场比机场还严格，赛过军事要地（笑声）。这次“作代会”，我想即使我当上代表，我也不去北京无聊了，恕不奉陪了！吃不到葡萄说葡萄是酸的？不，酸倒不酸，就是自大多了一点（笑声）。但现在我有点后悔，早知温总理在会上有这样精彩的讲话，我就放乖一点，给作协写封信，保证自己不再像上次“作代会”那样放肆了，保证在会上不胡言乱语了，保证在会上绝对不说真话只说假话了（笑声）。以此保证来争取当个代表（笑声），不当正式代表，列席的也行，去亲耳听听温总理的精彩讲话。 ( <a href="http://www.tecn.cn" target="_blank">http://www.tecn.cn</a> ) <br />
&nbsp; &nbsp; 温总理的这个讲话发表之后，好多朋友都说好。我认真读了，看了两遍，果然难得。好在哪里？好在不是官样文章，没有一点党八股。好在有真情实感，发自内心。好在它和以前在这种场合的讲话完全不一样。<br />
&nbsp; &nbsp; 怎么个不一样？ <br />
&nbsp; &nbsp; 一、以往凡是二、三、四把手讲话，一定要引用一把手的重要指示、经典语录，发表在报刊时，还要用黑体字突出出来。这是官场规矩，不可僭越。这次温总理的讲话，居然没引用当今一把手的任何指示和语录，只引用了早已过世却尚未过时的外国革命导师的语录。但他只用了马恩的，没用列斯的。这有很大区别和用意，我不细说。而且他引用的是马恩在《共产党宣言》中的一句话：“每个人的自由发展，是一切人自由发展的条件。”这句话是所有教条主义者、原教旨主义者最忌讳用的，但温总理引用了，他当然赞同这句话。引用革命导师的话，引用什么话，不引用什么话，大有学问，尤其是在中国，这也不细说了。总之，温总理的讲话，他所说的和所引用的都是自己要说的话。对比以前领导人在这类场合的很多讲话，有哪几句是出自自己真心的呢？有哪几句是自己的话呢？他们的讲稿，甲领导讲了，一字不改，乙领导也可以用；福建的领导今年讲了，辽宁的领导明年、后年、大后年也可以用（笑声）。千人一面，万口同声，毫无个性，绝无新意。因为他们说的都是政治教条。教条能有什么个性？教条能有什么新意？康师傅的方便面条也要比政治教条有些创意，每年还有些新产品哩（笑声）。<br />
&nbsp; &nbsp; 二、温总理在这篇讲话中，也有引用，甚至大量引用，但引用的是古今中外文化经典中的名人名诗名言名句。有《尚书》、《诗经》、《论语》、《孟子》《史记》中的名句，有屈原、苏东坡、郑板桥、林则徐、黄遵宪、艾青的诗句，有左宗棠的对联，有张载的座右铭，有赵丹的遗言，有康德的墓志铭，有亚当・斯密的一段话……在大约一万字左右的这篇讲话中，居然引经据典如此之多。是不是卖弄？是不是炫示？暂且不说，但他的积学之厚，腹笥之富，不得不使我这个中文系毕业生、戏剧学院研究生惊叹！我一直认为，一个政治家，有点人文素养，有点文艺底蕴，是个好事；接受过古今中外文化经典熏染的政治家，多少会有点人道主义，会有点悲悯情怀，懂得真假、善恶、美丑，就不会那么卑劣，不会那么残酷。这也是我能够接受温总理讲话的缘故。<br />
&nbsp; &nbsp; 三、温总理在讲话中，提到17位当代的作家、学者和艺术家的名字。重要的不在于提到了那些被主流肯定的人，更重要在于提到了那些敏感的、有异议的、如今已经很少被提起的人。比如，他提到了赵丹，这可是被某个左王诬蔑为“临死还放了个屁”的人。虽然温总理在引用赵丹临终遗言时，没有引用他那几句著名的“屁话”，但只要温总理提到他，总会让人想起那几句“屁话”，引起间接联想。温总理还提到吴祖光先生，这又是一个麻烦人物，胡乔木当年曾亲自上门劝他退党。虽然温总理提到他时一笔带过，更多的是提到和他共患难的妻子新凤霞，他说：“她是人民的艺术家，永远活在我们心里。”这难道不是同时在评价吴祖光吗？我有幸和吴祖光先生共同出访过三次，每天在一起，他真的是一个活得极为真实的人，让我崇敬。温总理还提到巴金。以往提到巴金的书，总是说他的《家》，然后说这部小说如何让读者走出封建家庭参加革命。而温总理这次提到的是巴金的《随想录》，须知这是一本有争议的书，出版之后即遭到批判，当时只能在香港出版。温总理说：“我读了受到极大的震撼，感到那是一本写真话的著作。”我不知道迄今为止，有哪位高位领导这样“极大”地评价过《随想录》！温总理还提到冰心，说：“她是一个有风骨的人。”“风骨”在中国当代语境中有特殊的含义，不是随便可用的，这是专指那些不畏权贵，铁骨铮铮，直言犯上，为民请命的人。你翻遍从毛到江的文集，有谁正面地使用过这个敏感的词！<br />
&nbsp; &nbsp; 四、温总理在这篇讲话中讲到文艺的功能：“提高人们的文化素质，激励人们的向上斗志，陶冶人们的道德情操，丰富人民的精神生活。”因此就要追求真善美。说到真，他说：“就是要反映真实，表现真情，追求真理。”还说：“在文艺界要提倡说真话，反映真实的社会情况……”他重提“双百”方针，重申要发扬创作自由和艺术民主……这些当然不是什么新的提法，不是什么新的政策。但他的新意不在于他说了什么，重要的在于他没说什么。他没说延安的那个讲话，没说“政治标准第一，艺术标准第二”，没说“团结自己，打击敌人”，没说“首先是党员，然后是作家”，没说文艺“是革命机器的齿轮和螺丝钉”，甚至没说“主旋律”。温总理能如此说，能如此不说，当然主要是时代的进步使然，和历史背景的转换有关，但和政治家本人的思想开明、见识通达不无关系，否则同在一个时代，为什么有些政治家就没有这样说和这样不说呢？甚至主管文艺和意识形态的政治家也没如此说和如此不说呢？<br />
&nbsp; &nbsp; 我想这也是温总理讲话反响强烈深获好评的原因。所以人们都期待着，在文学已下流、艺术已下贱的今天，温总理的讲话会带来新的生机。可万万没想到今年1月竟然发生了“禁书风波”，作者抗议，网民声援，国内新闻一律噤声，国外媒体纷纷报道，或震惊，或疑惑，但无不认为出版署的禁书和温总理的讲话精神完全是背道而驰；邬先生的禁书令，不但给书林蒙上一层雾水，还给总理泼了一桶污水，人们完全有理由担心妖雾又重来！<br />
&nbsp; &nbsp; 我这次来香港的前后几天，已经被《纽约时报》、《南华早报》、《明报》、《新报》、《东方日报》、香港特区政府的广播电视部、以及《亚洲之声》等多家媒体或请吃饭聊天，或正式访谈；回上海之后，英国的BBC也等着要采访我。其中记者提出最多的一个问题就是：温总理的讲话是否又是1957年的“阳谋”，是“钓鱼”，先是好话说尽，然后一网打尽？否则他的讲话犹言在耳，墨迹未干，而他所领导的国务院下属单位出版署就开始禁书，这如何解释？记者对我说，国外普遍的议论是怀疑温总理讲话的真诚，怀疑他的动机。 <br />
&nbsp; &nbsp; 我以为温总理的讲话不是“阳谋”，不是“钓鱼”，他是真诚的。 根据何在？ <br />
&nbsp; &nbsp;&nbsp;&nbsp;我的根据很感性，很直观，也许很幼稚，也许很好笑。 <br />
&nbsp; &nbsp; 我的根据就是：温总理会哭（笑声）！ <br />
&nbsp; &nbsp; 温总理大概是全世界最会哭的总理（笑声）。 <br />
&nbsp; &nbsp; 2003年4月25日在“非典”猖獗期间，在这极为艰难的时候，温总理会晤法国总理拉法兰。在欢迎仪式上，他看到冉冉升起的国旗，心中想到遭受病痛的人民，他默念着“愈挫愈勇，愈挫愈奋。”他双眼湿润了…… <br />
&nbsp; &nbsp; 2003年7月，温总理到江苏宿迁农村微服私访，未去政府机关，直接前往农田，一个农妇对他述说生活的困苦：一家五口，婆婆瘫痪，丈夫有病，两个孩子还小。全仗她一个人和三亩地。去年收入2650元，交税交了1460元，竟然交了一半多，此外还要让她这个一直住在农村的农民交纳城市建设税和政府招待税，不交就把他们全家赶出家门，还不给她两个孩子上学。这个农妇说着大哭，温总理也哭了…… <br />
&nbsp; &nbsp; 2004年底，铜川陈家山大矿难，166人遇难。2005年新年，温总理前去慰问，1月3号探访遇难者牛铁奇的家属，他握着牛的妻子和母亲的手，搂着两个年幼的孩子，流泪不止。这张照片传遍世界，温总理真诚的泪水感动了很多人…… <br />
&nbsp; &nbsp; 温总理为什么爱哭？还是用他引用过的艾青的诗句来回答吧：“为何我常噙满泪水o因为我深爱这片土地。” <br />
&nbsp; &nbsp; 我信任能哭的人。虽然这个世界越来越不相信眼泪，但我相信。我相信能够真诚流泪的人。我认为，能够大哭大笑的人，能够毫不掩饰自己真情实感的人，一般来说不太会欺骗，不太会虚伪，他们仁慈善良，有菩萨心肠。顺便说一句，如今一些当官的人，已经冷酷得不会哭了，甚至麻木得也不会笑了。当个科长还会笑，当了处长就开始板着面孔了（笑声），级别越高就越不笑了（笑声）。连笑都不笑、哭也不哭，你知道这些当领导的心里在想什么（笑声）？你能信任他们吗？所以我相信会哭的、有人性的温总理的讲话，他不是欺骗，是真诚的。<br />
&nbsp; &nbsp; 既然如此，为什么还有禁书之事呢？这是因为中国的政治形态正在转型，在这曲折的过程中，正声之外难免出现杂音。这是好事，好就好在有不同的声音，怕就怕在舆论一律，万马齐喑。如今不是说民主是个好东西吗？要民主就应该先从党内开始，先实施党内民主。党内民主重要的一条就是要在党内允许不同声音。毛泽东曾经引用过陈独秀的话：“党外无党，帝王思想；党内无派，千奇百怪。”党内有派，党内有不同的声音，应该是正常的、不奇怪的事情，否则就是一人作主，一人独裁，铁板一块，带来灾害。所以我们要善于聆听和分辨哪些声音是进步的健康的声音，并给予鼓励和支持，而不能不分青红，不分皂白，一律反对。<br />
&nbsp; &nbsp; 证实温总理讲话的真实无欺的另外一个根据是，“禁书风波”已有变化，我来香港之前就已听说，出版署的某人在新加坡的报纸上说他们没有禁书，而且还说从没禁过书。这当然是睁眼说瞎话！禁书不是今年才有，年年都有，哪年不禁？你出版署就是干这个事的嘛！否则发你们工资，让你们当官干什么？就我知道，上海多家出版社都遭到过禁书。所以出版署应该改名，改为“不出版署”（笑声）。现在出版署抵赖了，还连忙指示北京两家大书店，连夜将被禁的《伶人往事》陈列在书店主要展台上，大门外还拉出横幅，上书：“《伶人往事》在热销中（笑声）。”真是太夸张了，太明显的“此地无银三百两”了！他们企图以《伶人往事》的“热销”来“令人忘事”，忘掉禁书的事（笑声）！但是对这样的抵赖我们也欢迎，抵赖至少表明他们认为禁书是不得人心的，这比顽固到底，坚决禁书要好。<br />
&nbsp; &nbsp; 出版署的态度怎么会起如此变化呢？难道不是党内的另外一种声音在起作用？这声音中难道不包括温总理的声音？我相信这次“禁书风波”的事态还有可能往积极的一面发展。虽然这类事情的真正解决还得靠政治制度的改变，而不是靠“好皇帝”，不是靠“救世主”，但个人在一定的历史阶段中还是有特殊作用的。<br />
&nbsp; &nbsp; 我承认书生气十足，容易上当受骗。这次会不会又上当受骗呢？我想用我过去一首诗的最后一段做为我这次演讲的最后一段：<br />
