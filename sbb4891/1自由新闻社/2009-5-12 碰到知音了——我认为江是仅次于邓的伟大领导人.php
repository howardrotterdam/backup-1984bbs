&nbsp; &nbsp;沙叶新先生是感天动地的思想勇士，我以为他理当是二○○八年度“感动中国的人”。“男儿自有真”。从师从恩师黄佐临启蒙，到青出於蓝别树一帜，从话剧创作走向思想冲刺，从体制内党文化走向体制外自由共生，从回民传统走向普世价值，沙叶新不断刷新其真话真情真知真理的“挖祖坟”感召力。上世纪八十年代，他逆鳞“太祖”的代表作是钦禁话剧《假如我是真的》；新世纪，其血热中华的“命运交响曲”是疗治中国伪现代化后极权时代的“四化”雄文：《“表态”文化》、《“检讨”文化》、《“宣传”文化》、《“腐败”文化》；新近，更有“天谴论”、“毒奶问”让天下风走而响应……<br />
