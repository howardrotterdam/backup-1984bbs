<br />
&nbsp; &nbsp;朱：董健先生在总结三十年改革和大学教育现状的基础上，对大学官僚化、学术腐败进行有力鞭挞。<br />
沙：学校应该是一个很神圣的地方，连学校都腐败，你说中国还有什么希望，培养什么学生啊！<br />
<br />
