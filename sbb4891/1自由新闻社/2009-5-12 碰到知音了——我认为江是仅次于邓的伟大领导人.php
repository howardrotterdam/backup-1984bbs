醉生梦死 @ 2009-5-12 19:03 评论(0) 推荐值(0) 引用通告 分类: 胡说八道<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;依着我这几年的见识――也许我的见识很浅陋，大家对胡锦涛政府的满意程度要远远高于江泽民政府。一提起江泽民，骂声铺天而来，掷地有声，并且一个给他说好话的人都没有；可是一提起胡锦涛，虽然骂声也有，但是和江泽民的骂声比起来，简直就像蚊子叫，并且说胡锦涛政府好话的人，绝对比骂的人要多，尤其是温影帝的几场绝妙演出之后。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;以上就是我奇怪的地方，我不知道大家是怎么比较出胡锦涛政府比江泽民政府好的。难道八荣八耻比三个代表更有什么实际内容吗？难道就因为胡锦涛政府取消了农业税？可是一老农民和我说，农业税取消了，化肥涨了，一加一减，最后还不如不取消农业税呢。难道就因为胡锦涛政府调高了个税起征点？但是，好像通货膨胀率比征税率涨的快多了。还有什么看起来很美的事情，我就不知道了。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;胡锦涛政府执政期间，刚一上台爆发非典，其无视公民生命安全，隐报瞒报不作为和江泽民政府没任何区别；四川大地震，贪污救灾物资一样的明目张胆，被人发现了，就耍赖，睁眼说瞎话。用共产党的传统方法给脑残洗脑，两个政府也是别无二致。所以，我真的不明白，为什么会有这么多人认为胡锦涛政府更好一些呢？<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;从我个人来说，我更喜欢江泽民政府。是的，那个时代一样的黑暗，一样的民不聊生。但是，江泽民时代的《焦点访谈》至少还会接露阴暗面，虽然攻击的最高级别也就到县，但那也是县啊，也是共产党的一个零件啊！你啥时候见胡锦涛时代的《焦点访谈》接露过什么？别说县，连个村子都没接露过。除了赞歌就是赞歌，每天看完《焦点访谈》才敢吃饭，不然全吐了。九八年洪水，一样有不长眼的货色明目张胆的贪污救灾物资，可是在救灾完毕后，别管多少，也别管官大小，至少有被彻查的，有被撤职的，有被逮捕的。四川大地震，胡锦涛政府除了用军队镇压因为政府官员的贪污而暴动的人民以外，还干了什么？山西黑砖窑的事情，才多长时间就都官复原职了？肯定破了江泽民时代的记录了。坏事江泽民时代共产党也在干，但是似乎大约好像还有那么一点羞耻心，比如镇压上访群众，悄悄镇压也就完了，从没像胡锦涛政府这样不知廉耻的说上方的人员百分之九十九是精神病。――从这个事情看，胡锦涛政府的智商都没江泽民政府高。被一个傻子政府统治，还是一个干尽坏事的傻子政府，真令人遍体生寒。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;从1949年开始，真正掌握了国家大权的人，有毛泽东、邓小平、江泽民、胡锦涛，比较一下这四个人的事迹，再比较一下四个人执政期间的国家，对人民来说，难道江泽民不是仅次于邓小平的伟大领导人吗？<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;虽然大江已经东去，成了老江，而且墙倒众人推，人人都嘲你笑你骂你，但是我绝对相信你支持你。虽然你也是个王八蛋，虽然你执政的时候社会也是极端黑暗，但是在一个永远不可能有光明的国度里，我们唯一的奢望就是百分之九十九黑暗就行了，别他妈玩百分之百，而你，正是带给我们百分之九十九黑暗的那个人。胡锦涛他有什么啊？除了不像你对唱歌那么变态的执着以外，哪点能比得上你？凭什么比你受爱戴？我真为你感到不公平！</div>

							
							
							
							
