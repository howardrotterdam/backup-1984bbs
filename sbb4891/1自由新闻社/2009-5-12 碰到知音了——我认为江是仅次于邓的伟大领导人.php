<br />
&nbsp; &nbsp;沙：他那时候说，在上海只认识两个作家：一个白桦，一个沙叶新，这是他亲口讲的。<br />
<br />
&nbsp; &nbsp;朱：他似乎知道你的分量与影响，但后来真心喜欢信任的却是“余含泪”之流。如若他真心敬重你这样敢於说真话的“童心小孩”，中国今天可能不至於全国食品一片毒，毒奶毒蛋遍地了，“三个代表”带来了三聚氰胺！<br />
