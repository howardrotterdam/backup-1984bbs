胡温时期，党倒成弱势了<br />
<br />
&nbsp; &nbsp;沙：政治上的三工XX，文化上的某某某（余秋雨）是一丘之貉。<br />
<br />
&nbsp; &nbsp;朱：你这话太精V了！如果客观地比较，胡温时代和江时代，到底哪个要强一点呢？有人说，可能江时代还稍微强一点，你看呢？<br />
<br />
