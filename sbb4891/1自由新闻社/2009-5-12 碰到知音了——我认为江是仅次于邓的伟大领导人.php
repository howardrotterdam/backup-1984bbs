&nbsp; &nbsp;沙叶新打破将艺术、学术与思想探索对立的二元论，既不排斥“身衣学术的华衮，粹然成为儒宗”，也没有“为昭示后世计，自藏其锋芒”，对李慎之、王元化皆心仪不已，一面以《风雨苍黄五十年》为旗帜，跳出壕堑大写抨击时弊“四化”雄文，一面继续创作《江青和她的丈夫们》、《幸遇先生蔡》等新话剧，以《中国新文学大系》第四辑戏剧卷主编主持三十年话剧改革研究，沙先生可谓融作家、学者与战士於一身，文武双全。<br />
<br />
&nbsp; &nbsp;即便有了这样强烈的感动背景，近日与沙叶新先生相见，仍然又有刻骨铭心的新感动，油然“喜此时幸遇先生沙”。沙叶新有话剧名为《幸遇先生蔡》，说的是蔡元培创立北大精神的故事，剧名借用老“北大校歌”之歌词。<br />
<br />
&nbsp; &nbsp;此时，读过余秋雨的“含泪”说，看过其食言“拒绝一切官方荣誉”而伏谢钦赐“大师”称号，自我宣选坝嗲镉旯示游国家文物保护单位”，上海文化界的三千年未有之犬儒让人何等悲哀！但二○○八年十月十四日下午，这一悲观得到改变，其时到上海探望病中的沙叶新先生，两小时开怀放谈，一次次深深敬U.我想，沙叶新先生对上海文化界犬儒化的力挽狂澜，也足以让上海人“喜此时幸遇先生沙”。<br />
<br />
&nbsp; &nbsp;民谚：“一娘养九子，九子九个样”，大上海岂能任由“含泪的余秋雨”邀宠摇尾？在“自由朱学勤”之外，还有“独立沙叶新”！二人联袂冒险发表“天谴论”，狙击“余含泪”御用新潮，朱、沙二人似乎决意构建上海新的“自由精神双子座”。<br />
<br />
&nbsp; &nbsp;与历史照片比，年近七十的沙叶新瘦了不少，但与动手术和化疗前相比，只是面色略有疲乏，好在双目仍炯，壮志依然。依旧幽默，依旧俏皮，依旧勇於“图穷匕见”，真知灼见，“大珠小珠落玉盘”。好汉不提当年勇。三十年锐气不减，三十年自由不羁，这才是“我心永远”！一个人一时智勇不难，难的是三十年始终锐不可当！这才是路遥知马力，这才是百炼成钢。回顾起来，沙叶新先生让我感动的新细节，历历在目。<br />
<br />
江泽民时代败坏整个中华民族<br />
<br />
&nbsp; &nbsp;沙叶新先生自以话剧《假如我是真的》轰动海内外起，就以说真话而闻名於世。连名片上都是别具一格的真话：“沙叶新──上海人民艺术院院长──暂时的；剧作家──长久的；某某理事，某某委员，某某教授，某某主席──都是挂名的。”<br />
<br />
&nbsp; &nbsp;只有相逢说真话者，才会有强烈的“读书不如访谈”之感慨。当说到前中共三工总书记时，沙叶新竟然毫不留情地说：“三工XX时代败坏了整个一个（中华）民族！”──一个新感动顿时流遍我的全身：沙叶新还是一个体制内的中共党员啊！以他在体制内六十年的体验，以他的大智慧，不可能不知，江虽然退下来了，其实还有太上皇之威啊！纵然沙叶新的儿子在美国，可他和夫人还生活在“上海帮”严密控制的上海滩啊！如此逆鳞太上皇，要准备担当多大风险！难怪有人怀疑，发病之前还能在冬天洗冷水澡的沙叶新，是否因为“过激言论”而“病从口入”呢？<br />
<br />
&nbsp; &nbsp;他明知这不是闲聊，而是接受正式穹茫面对着摄像机、录音机和照相机，如此清晰地“犯上”，且不断重}，这可是“文死谏”者也要犹豫的。沙叶新如此“苟利国家生死以，岂因祸福避趋之”，不啻是对天下犬儒焦大的当头棒喝！除了“余含泪”之流，谁能不感动？<br />
<br />
&nbsp; &nbsp;在震撼人心的《“腐败”文化》中，沙叶新曾手捧赤心：“上海是一个虎穴，还有一个更大的虎穴是政治局。有人吓一跳，说我胆敢怀疑政治局？我说，为何不能怀疑政治局？他说，这种话你怎么能说？我说，为什么不能说？闻一多有首诗：「有一句话说出来就是祸，有一句话能点得着火。」如果一句话能点起反腐的正义之火，即便我身陷火海，祸及自身，也在所不惜。中国总要有人说话，哪怕五千年都没有说破，如今也要说；如果不说，谁能猜得透火山的缄默？如果火山一旦爆发，那就不是我一人之祸，而是国家之祸，是民族之祸，是苍生之祸！”现在他说“三工XX时代败坏了整个一个（中华）民族”，再次证明他英雄到老不入佛！英雄虽病刀不老！“一个不允许别人对他说「不」的人，绝不可交；一个不允许别人对他说「不」的民族，也难以为友。”沙叶新有此名言，自然批评无禁区，对任何人都敢於说不。哪怕是昔日同学好友！哪怕是今日太上皇！哪怕是“六三六五”时坦克隆隆，他也为死去的无辜者公开带了三天黑纱！<br />
<br />
&nbsp; &nbsp;沙叶新志愿“天下无敌”，从不将任何人视为仇敌，他充满医治一切“精神病人”的“悬壶”情怀。<br />
<br />
上海盛产“二丑艺术”<br />
<br />
&nbsp; &nbsp;为何“上海是一个虎穴”？其时我们谈到上海文化和上海文化人，对话是这样的──朱：你对上海文化界改革开放三十年怎么看？为何“上海是一个虎穴”？<br />
<br />
&nbsp; &nbsp;沙：上海不是领时代之潮流的，至少这三十年，它是起了个坏作用的。上海是一个商业城市，他们都说我有一句非常精彩的话，包括上海文人，上海人他只做无本的生意，只敢於做无本的生意。<br />
<br />
&nbsp; &nbsp;朱：上海文化人首先选择保险、安全、有大利益的事情？<br />
<br />
&nbsp; &nbsp;沙：上海人不愿意做无利的买卖，他明哲保身，他要将本求利，他要精打细算。他只能是什么呢？他只能是这样做，包括上海文人也是这样的：他可以在文人面前讲讲官府怎么样，在官府面前他可以骂骂文人，哎呀！这人怎么怎么！<br />
<br />
&nbsp; &nbsp;朱：就是鲁迅说的“二花脸”那种人？上海盛产“二丑艺术”？<br />
<br />
&nbsp; &nbsp;沙：叫“二丑”嘛！鲁迅先生就是在上海写的《二丑艺术》嘛！这个现在也是这样的啊，（上海文人）他假如说要敢说话的时候，他有两个条件：一个条件就是我是安全的，其次是一定要有利益，利益第一。你要妨碍我的利益，那就不行了。这是很典型的上海人，这就是上海这个城市。现在这种体制之下，上海同是一个太阳照着，同是一个党领导，在这样的后极权时代，你说它怎么可能领时代之风骚呢？！不可能的。就是像我这样的人，也根本不值得一提的，大家都说我了不起什么的，是因为上海整个的文化土壤、空气一塌糊涂，才显得我像芝麻里面的一颗绿豆。不，不合适，我不过是绿豆里的芝麻一样。<br />
<br />
&nbsp; &nbsp;朱：你这样谦虚！你既不是生在上海的上海人，也与你所说的许多上海文人完全不一样。鲁迅说皮日休是“一塌糊涂的泥塘里的光彩和锋芒”，你是上海这个“一塌糊涂的泥塘里的光彩和锋芒”！<br />
<br />
&nbsp; &nbsp;沙：我认为上海它总体上是这样（一塌糊涂的泥塘）。<br />
<br />
&nbsp; &nbsp;朱：虽然你遵从“连眼珠也不转过去”的轻蔑法，不愿公开提及“余含泪”，但你关於上海文人做事一求安全二图大利的两点概括，可能真正抓住了余秋雨之类上海文人的本质了。<br />
<br />
&nbsp; &nbsp;沙：当然这和整个国家都有关系。九十年代之后，由於共产党为了使自己的合法性能站住脚，小平就讲“停止争论”，他就是想要把经济搞上去，来取得它的合法地位。但是，后极权时代一个最最明显的特徵，就是政治上（的专制）它绝对不会放弃，尽管它已松动很多了。<br />
<br />
&nbsp; &nbsp;朱：就是说，中共已从以信仰、理想、民主、公平为立国之本，衰落为以经济建设的成效作为它的政权合法性，即使这种“经济建设”以牺牲当代百姓的自由与健康，牺牲未来子孙的立足之地为代价，也在所不惜。<br />
<br />
&nbsp; &nbsp;沙：那么这就会影响整个社会，影响整个臣民吧，它这个目的是明确的。於是对知识分子的笼络、拉拢、腐蚀，在九十年代是最为严重的，高校的腐败都是骇人听闻的。<br />
<br />
&nbsp; &nbsp;朱：当年以反右、文革残害知识分子，是政治腐败；现在以高薪奖金博导教授等等名利腐蚀知识分子，是经济腐败，六十年来的新中国，知识分子始终在遭受腐败专制的摧残。现在的高校腐败其实是执政党毒害知识分子的标志。<br />
<br />
&nbsp; &nbsp;沙：是啊，今天的知识分子，包括高校的知识分子，本是文化的传承者，但这些人都腐败了，还有什么文化可言哪？！<br />
<br />
&nbsp; &nbsp;朱：上海的高校腐败与外地相比，有什么特点呢？<br />
<br />
&nbsp; &nbsp;沙：我没做过比较，但是我非常关注这个事，前不久跟邓正来先生在一起吃饭，他专门跟我讲了这个高校腐败，所以我也很关注。今年我在香港做了两次演讲，一次是给香港教会大学，一次是给尖沙嘴的文化中心，其中都涉及到教育腐败，但是我这还是只能叫“隔靴搔痒”，因为我毕竟很久没有去过大学，但是他们是个中人啊，都是在什么吉林大学啊、什么北京大学工作过的，他们讲起来是有根有据的，那真是骇人听闻。邓正来先生讲，在高校，首先（研究课题）项目权是掌握在领导手里，你是政治上可靠、听话的人，就把项目给你，这些项目有些几百万，有些几十万的。<br />
<br />
&nbsp; &nbsp;朱：项目的承担者就可从项目经费中得到出国等等好处，就像官僚一样可以公款报销“吃喝嫖赌”。现在高校通过这种项目腐败来笼络和牢牢控制一些知识分子。许多犬儒和“焦大”，就是这样产生的。<br />
<br />
&nbsp; &nbsp;沙：还有很多手段，项目是其中之一，还有职称。<br />
<br />
&nbsp; &nbsp;朱：对，还有职称，一个博导，就可让好多人变节投降。<br />
<br />
&nbsp; &nbsp;沙：这个我不是专业的，你在网上可以搜一搜。职称它是这样有牵制力，子女也那个样，你的子女能不能读大学，是每个教授、每个知识分子都切身关心的。<br />
<br />
&nbsp; &nbsp;朱：是啊，像赵紫阳那样甘愿牺牲家人的确实少，连胡耀邦也因考虑到儿子的仕途，而不得不违心做检查。<br />
<br />
&nbsp; &nbsp;沙：哎，你子女读哪个学校都是有关系的。评职称也是这样的，你说，现在怎么博导那么多？！<br />
<br />
&nbsp; &nbsp;朱：今天的整体腐败可说是三千年未有之变局！<br />
