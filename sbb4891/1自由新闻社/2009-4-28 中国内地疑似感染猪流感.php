<br />
　　4月19日，国际货币基金组织下调了对全球经济的增长预期，预计今年全球经济将下滑1.3%，而1月，这一预测值为0.5%。市场担忧，如果猪流感疫情持续蔓延，全球经济将面临更多困难。<br />
<br />
　由于猪流感疫情尚处发展初期，对全球经济的整体影响还很难估算。<br />
<br />
　　4月26日，墨西哥财政部长阿古斯汀・卡斯滕斯（Agustin Carstens）在华盛顿出席世界银行和国际货币基金组织春季年会时表示，猪流感疫情的爆发，可能对经济产生重大影响，“非常有可能导致经济活动中断，但要预测程度如何，还言之过早。”<br />
<br />
　　对于猪流感是否将放慢美国经济复苏的步伐，国际货币基金组织（IMF）执行总裁多米尼克・斯特劳斯-卡恩（Strauss-Kahn）4月28日在华盛顿接受《财经》等三家中国媒体联合采访时称，他自己并不具备足够的知识与经验解答这一问题，但可以肯定的是形势十分严重，如有需要，IMF将一如既往地与世行一道对成员国家提供支援。<br />
<br />
