金羊网 2009-04-29 08:11:45 <br />
 <br />
昨日，广州中医药大学附属第一医院温病教研室主任、博士导师钟嘉熙教授根据媒体公开报道的人感染猪流感症状推出防治猪流感药方，今天该院还会在门诊部免费派发该药方煮出来的凉茶。 <br />
<br />
钟嘉熙教授表示，猪流感为流感的一种，因此早期症状相似。根据公开报道的症状，猪流感属于中医温病范畴，表现为“ <br />
