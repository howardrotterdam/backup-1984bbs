<br />
　　世界银行负责拉美和加勒比海地区健康事务的部门经理凯・汉森（Keith Hansen）表示，世界银行对猪流感可能使经济衰退进一步加剧感到非常担心。之前曾经发生过疫情使当时的贸易、航空和旅游减少，市场活力也暂时出现衰退的情况。<br />
<br />
　　凯・汉森认为，疫情对经济的影响程度如何，取决于疫情的规模、种类和持续的时间，现在预测，还为时过早。“不过有一个好消息是，短期的疫情对经济影响似乎很有限。”<br />
