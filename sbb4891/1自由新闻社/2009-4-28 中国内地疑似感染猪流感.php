<br />
　　英国政府警告其公民避免去墨西哥，在确诊首例猪流感病例后，在入关口加强了安全检查，严防疫情流入；日本外务省要求国民在“没有必要和紧急的情况下”暂缓前往墨西哥；古巴和阿根廷分别宣布暂停与墨西哥通航或禁止来自墨西哥的航班降落。<br />
<br />
