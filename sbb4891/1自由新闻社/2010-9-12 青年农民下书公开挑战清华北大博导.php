																											<div id="postmessage_826329" class="t_msgfont">青年农民下书公开挑战清华北大博导(图)2010-09-09 08:50:00　来源: <a href="http://edu.anhuinews.com/system/2010/09/09/003351172.shtml" target="_blank">大河报</a>(郑州)　<a href="http://comment.edu.163.com/education_bbs/6G4JUN4M00293L7F.html" target="_blank"><font color="#ba2636">跟贴 35 条</font></a> <a href="http://help.3g.163.com/" target="_blank">手机看新闻</a> <br />
核心提示：仅有初中学历的台前县打渔陈乡青年农民姜英称自己于9月1日在北京向清华、北大发起挑战，将自己的授课费大胆定位在每小时130万元。此举引起诸多争议。<br />
<img src="http://img1.cache.netease.com/catchpic/D/D4/D40BC082E66D17E817265859096E1663.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
青年农民姜英伟<br />
 　　<strong>大河报报道：</strong>扬言以自己研究的理论，可在5分钟内难倒清华、北大的博士生导师。并称，如有条件，还可挑战牛津、剑桥大学等世界知名学府。9月6日，仅有初中学历的台前县打渔陈乡青年农民姜英伟致电本报，称自己于9月1日在北京向清华、<a href="http://edu.163.com/special/00293U7P/2010zizhuzhaosheng.html" target="_blank"><font color="#1e50a2">北大</font></a>发起挑战，将自己的授课费大胆定位在每小时130万元。此举引起诸多争议，他也被网民称为“挑战哥”。<br />
　　<strong>下挑战书公开挑战清华北大</strong><br />
　　9月1日下午，距清华大学东门约300米处，27岁的姜英伟在路边竖起一块牌，并向过往行人发放“挑战书”，称欲通过讲授自己研究的促进社会发展的理论，挑战清华、北大的博士生导师，授课费每小时130万元。此事经北京部分平面媒体报道后，立即被中新网、新华网等10余家网站转载。<br />
　　昨天上午，记者和姜英伟进行了一次长达半小时的对话。<br />
　　<strong>放弃<a href="http://edu.163.com/special/0029rt/zikaolist.html" target="_blank"><font color="#1e50a2">自考</font></a>自费研究“理论”</strong><br />
　　姜英伟说，他是台前县打渔陈乡农民，仅有初中学历，因家境贫寒，高中仅上了半年就退学了。打工后，他自考行政管理专业，已考过了六科。在多次听教授“照本宣科”式的讲课内容后，他认为这是“伪<a href="http://edu.163.com/" target="_blank"><font color="#1e50a2">教育</font></a>”，就选择了退学。<br />
　　在江苏打工期间，他读了很多书。今年7月底，经过数年的努力，他写的“共产主义中国造”书样稿完成，书中罗列了他对共产主义理论的一些看法。<br />
　　<strong>作秀还是炒作？ “挑战哥”惹争议</strong><br />
　　8月中旬，姜英伟以特快专递形式给清华、北大递交“挑战书”，这两所学校虽然都签收了，却一直没有回应。8月底，姜英伟来到北京，在清华大学附近挂起牌，高调宣传自己的“挑战书”。<br />
　　“挑战清华、北大不是唯一目的！”姜英伟称，他要挑战的是整个学术界，或者叫教育界。<br />
　　在网络上，众多网民对姜英伟的举动褒贬不一。<br />
　　支持者称：“太强了，勇气可嘉，希望能够挑战成功。”<br />
　　反对者则说：“蚍蜉撼大树，可笑不自量。”还有网友说：“一看就是炒作，无聊。”<br />
　　<strong>他想这样挑战</strong><br />
　　记者：你如何挑战清华、北大教授？<br />
　　姜英伟：我已向这两所国内最知名的学府发送了“挑战书”，挑战时间是9月上旬。我的挑战口号是：“给我三尺讲台，我让中国进步200年，让世界进步600年。”<br />
　　记者：你用什么本领来挑战两所大学的教授？你的理论依据是什么？<br />
　　姜英伟：我特别爱看书，在江苏从事策划工作的数年间，我不仅积累了社会阅历，更有机会对社会科学、人文科学等诸多学科书籍进行了阅读和深入思考。比如《六韬》、《道德经》、《庄子》、《理想国》、《周易》、《毛泽东文选》《邓小平文选》、党的十七大报告等，我不仅熟读，而且还对其精神内涵有着自己的深刻理解。<br />
　　我的理论依据来源于我对知识的研究与积累，这与大学教授单纯性“照本宣科”有巨大差别，我是针对一个问题进行彻底剖析，直到弄明白为止。<br />
　　记者：你与两所大学教授挑战的内容是什么？<br />
　　姜英伟：为了此次挑战，我准备了20道简答题，内容涉及人类学、社会学、物理学、政治学、哲学、法学、教育学等。比如：“宇宙形成的原动力是什么？”“‘实事求是’的终极目标是什么？”及“天人合一”的思想精髓是什么等，这些问题都很难回答，任何一个大学教授都不会答得十分透彻。<br />
　　最后，姜英伟称，自己目前工作不错，收入也高，之所以这样做，主要是想让中国的大学教育工作者们认识自身存在的不足，在实践中加以改正后，更多更好地为中国培育尖端人才。<br />
