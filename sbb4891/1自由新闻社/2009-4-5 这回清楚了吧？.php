 <br />
<br />
六三六五堵军车，自绝生路<br />
<br />
马悲鸣<br />
<br />
现在包括封从德、芦笛在内，都指责当年因为学生市民是非武装的，所以军队不该开枪。<br />
<br />
这事得这么讲。西方非武装示威如果非法的话，警察会把他们用塑料手铐铐起来一个个地带走。这次纽约反战示威就是鲜活的例证。他们的最大耍赖就是躺倒不动，任凭警察将他们抬走。但如果反抗，警察自有法定的暴力升级制服手段随后。当年造成洛杉矶暴乱的起因，就是因为黑人酒鬼金恩的抵抗遭到警察暴力升级制服时的场面被拍了下来播放。<br />
<br />
西方社会因为大家都忙，这种非法的非武装躺倒示威参加者人数有限。市区警察只要有足够的人手或力气，就能把他们一个个抬完。<br />
<br />
但如果人数太多，警察人手不够的话怎么办？<br />
<br />
很简单，将示威区封锁起来，只许出，不许入。警察一个个地将非法示威者抬出封锁区外，或者直接送往监狱，不许他们返回重新加入示威行列。<br />
<br />
但如果非法示威者的后援不断的话怎么办？而后援人数多到一般的封锁线阻拦不住怎么办？<br />
<br />
你这里刚抬出一个，他们那边冲进来两个。结果是越往外抬，里面的人越多。<br />
<br />
下一步的措施就是增加警力，建立警察人墙阻挡后援冲入。<br />
<br />
如果再阻拦不住的话，就可以使用法定的后续升级暴力制服示威者。最终直到宣布该地区进入紧急状态，调集国民警卫队或海军陆战队开枪。<br />
<br />
六三六五已经过去十四年，如今参与者还在一边津津乐道地赞美堵军车壮举的同时，一边指责清场部队开枪。而其实这两个事件是有因果关系的。只要没堵住军车，便不会开枪。<br />
<br />
5月13日绝食开始割据广场，政府尽力低姿态希望能和平结束事态，竟至于有史以来头一次宰相亲自出面满足非法割据一方的对话要求。在这一切全部失效以后，因为参加者场面过于浩大，非警力所能制服，只有宣布戒严令。相当于西方国家的紧急状态法。<br />
<br />
最初调集军队的目的其实并非象洛杉矶暴乱那样直接了当地攻入，否则早就开枪了。当时调集大规模军队的目的很明确，就是希望用足够多的军队组成有足够抵挡冲击力强度的人墙包围圈，将已经在广场上的人群包围起来。这样才能开始一个一个地往外抬人。抬出一个少一个。到那时候，军车和坦克自然也会被用做建立包围圈的材料，以加强包围圈的强度。<br />
<br />
只要这个包围圈建立不起来，则在当时的情况下，不可能用警察或徒手军人将这些广场上的占领者抬出去。假如他们都听话，绝对不反抗的话，也不可能。因为后援不断，而且没有包围圈，刚被抬出去的人也会返回来重新加入。<br />
<br />
这个设想完全符合西方国家政府对付示威者的人道主义精神。<br />
<br />
但调军者没有想到的是，忽然跑出来一群市民，把军车堵住，无法通行了。僵持了很长一段时间后，军方无法，只好撤退。<br />
<br />
我看着市民和占领广场的学生欢呼堵军车的胜利时想到一句歇后语∶望乡台上唱山歌，不知死的鬼。<br />
<br />
市民把军车堵住，就绝了自己的后路，擎等人家打活靶了，而且后果自负。<br />
<br />
从军车撤退到六三六五开枪之间还有一周多的空隙。这期间政府曾多次派非武装整建制连队试图强行切入，其目的很明显，最初仍是想尝试重建包围圈。后来则是有点强行切入抓获割据首领以瓦解广场占领者士气的味道。结果是所有强行切入全都被堵了回去。而每次切入失败，都从反面鼓励了割据者的信心。到此，就连尝试擒贼擒王都成了反助对方士气，失去了意义。<br />
<br />
最后戒严部队只好不做任何动作，用疲兵之计等待广场占领者因困乏涣散而自行离去。<br />
<br />
但经过多日观望，连这也不行。因为第一是外地学生听到消息已经开始行动增援广场。港支联的占领器材，如帐篷等也已运到。这使得广场占领者有了无限的后援，可以经年长久地割据下去。<br />
<br />
第二是占领当局开始寻求合法性支持，希图利用人大紧急会议或即将到来的常委会议的表决使割据合法化。<br />
<br />
第三，也是最重要的，刘晓波重新发起的绝食正在挑起新一波的高潮。<br />
<br />
当然还有第四、第五、第六。<br />
<br />
所有这些加起来，彻底断绝了和平结束是次天安门事件的任何希望，迫使戒严当局不得不下令不惜任何代价清场。<br />
<br />
刘晓波是压断骆驼脊梁的最后一根稻草。这一点从他在卡玛的电影里接受的采访中也能看得出来。<br />
<br />
刘晓波对最后逼成非开枪不可的局面负有不可推卸的责任，而且不仅是道义的。<br />
<br />
造成如此严重后果的刘晓波自称，因为他的出面谈判而导致没有发生总攻的大屠杀。其实这只是他那小知识分子自大狂式的幻想。<br />
<br />
清场部队进入广场四周并未发起总攻，而是将广场包围了起来。由此可见，部队的目的仍是最初的设想，先形成对割据者占绝对优势的包围圈，然后进去一个个地往外抬人。但由于刘晓波的谈判，答应马上撤退，所以部队的行动便到此为止。<br />
<br />
刘晓波谈判的功劳也只不过是最后一根稻草。军队根本就没有总攻的打算。<br />
<br />
这就是六三六五事件的基本真相，可分十步看。<br />
<br />
非法割据第一，<br />
<br />
政府屈身对话无效第二，<br />
<br />
戒严第三，<br />
<br />
堵军车第四，<br />
<br />
派小股部队尝试强行切入第五，<br />
<br />
外地学生和港支联的后援开始出现第六，<br />
<br />
刘晓波再掀波澜第七，<br />
<br />
军队被迫强行推进，途中遭遇抵抗，不得已开枪打死了抵抗者并误伤旁观群众第八，<br />
<br />
军队形成对广场的包围第九，<br />
<br />
刘晓波和学生吓坏了，只好求饶撤退第十。<br />
<br />
这里王丹、郑义的责任在造成非法割据第一步。柴玲、刘晓波等人的责任在后来的几步。<br />
<br />
顺便说一句，坦克是用做防范挨揍强行推进用的。既不怕石块打击，也不在乎水泥路障的阻拦。所以坦克只用了其装甲和发动机部分，未用武装。坦克轧人大概属于因为潜望镜视野太窄，驾驶员没看见所致。<br />
<br />
后来“王维林”从正面挡坦克，驾驶员看到了，便躲避没轧。这说明军方并没有坦克可以轧人的命令。<br />
<br />
六三六五使中国政府的形象损失极大。开枪完全不符合政府利益。他们只希望尽可能低调和平地平息事态。但学潮一方不允许。所以才造成如此严重结果。<br />
<br />
根据封从德承认的原则，如果两事件之间有因果关系，则先发生的是因，后发生的是果。从前述导致的六三六五死人的十步看，责任全在广场占领当局。<br />
<br />
这回清楚了吧？</div>

							
							
							
							
