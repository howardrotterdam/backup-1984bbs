又到了这个季节，再谈谈8*8（不是八心八箭。。。）。<br />
<br />
这是个不会结束讨论的事件，像其它很多事件一样，会有人真正想要传播真相，仅仅出于自己的良知；不可避免的，也有人利用此事达到自己的目的。对于前者，由于过程复杂，或不了解全部，在其叙述中免不了带有感情色彩和偏见；而后者，则又一定会忽略部分事实，故意引导众人，以达到目的。<br />
<br />
讨论如此的话题本身就容易缺乏理性，其敏感程度又让那些遭受信息封锁进而不相信官方说辞的年轻人更容易认同其它方面的描述，也就更容易受到第二种人的煽动了。<br />
<br />
这显然不是政府愿意看到的，也恰恰反映出政府对待此事件的不当之处，正如藏独问题，封锁传媒仅一方报道，怕是不妥吧？这样看似控制了媒体舆论，范围却也仅就国内大部分地区而言，在国际上是没有朋友的，外交上遭受被动。维持国内稳定不错，但中国的发展终究是面向世界的，8*8时邓小平也说过，这是国际大气候和国内小气候共同造成的，由此来讲不能不考虑国际因素。<br />
<br />
鉴于此，我们应全面地、理性地来看8*8。不能单看现场照片来谈社会乃至国家存在的问题。这么说不是对死者的无动于衷，出现死伤的结果是谁都不愿意看到的，反而更促使我们去思考，究竟为何会如此，到底发生了什么？<br />
