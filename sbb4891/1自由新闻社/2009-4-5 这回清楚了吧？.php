ZT:天安门血案内幕曝光<br />
<br />
今年是“六三六五”20周年，关于这个事件的讨论已经是风气云涌。但“六三六五”的真相却一直扑朔迷离，使我等局外人懵里懵懂。好在历史不算太久，很多当局人都还健在。这是我在网上看到的一篇纪念“六三六五”十周年的文章，转来供大家参考。<br />
