通过市场来解决这些问题的核心是，它不期望彻底消除任何一种“罪恶”；而不是像政府一样，通常是声称要彻底解决某个问题，但是从来都做不到。市场介入司法，它承认社会上可能有各种各样的角落，当中存在大多数人看不惯的罪恶。但是越试图进一步缩小它的地盘，所耗费的成本就越大。到最后成本达到整个市场无法承受的程度，消除罪恶的努力就会止步。此时，邪恶与正义达到一种社会可以接受的平衡。这个过程可以用市场原理来理解，把“消除罪恶”看作是一种商品即可。我们对“消除罪恶”的需求越多，生产它的成本就会越高，而给我们带来的边际效益也会越低；到最后理性的消费者就会感到，继续投入“消除罪恶”不是一个划算的买卖，就会停止。而当罪恶开始泛滥，那么“消除罪恶”的边际成本相对较低，而边际收益较高，人们就会倾向于投入。如此循环，以致平衡。<br />
<br />
罪恶总会存在，这是我们所必须接受的现实。在接受了这个现实之后，我们再来谈“用市场解决司法、执法”问题。<br />
<br />
由政府来处理、或者说由强制税收作为财力来源的司法、执法的问题是，它经常耗费过多的、超出必要程度的成本，来试图铲除罪恶（暂且不谈腐败、缺乏竞争机制等问题）。这一后果，与政府强制介入其他市场时所产生的效果是相类似的，即定价的人为操纵化。<br />
<br />
有人一定会说，人的生命是无价的，小女孩儿的贞操是无价的，如果是你的女儿被裸照了你会怎么想，诸如此类。所以我们在执法的时候不应该谈钱，不应该考虑成本，要不惜一切代价。但事实上政府执法的时候它也是要考虑成本的。比如，如果你老婆走失了，公安不会发动全国的警察来帮你找。这在任何一个国家任何一种制度下都是不可能的，为什么？成本太高。哪怕是政府主导的司法、执法，它也必然涉及到成本控制问题。既然考虑成本是不可避免的，为什么不通过市场来调控司法成本？</div>

							
							
							
							
