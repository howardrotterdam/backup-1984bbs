													<h2>跟随党路线 人大报告尽显「中国特色」</h2>
						
						
																											<div id="postmessage_511558" class="t_msgfont">明报网上版 2010-03-10<br />
<br />
<br />
【明报专讯】相较于「政府工作报告」、最高法院和最高检察院的工作报告，人大常委会的工作报告往往被认为最不重要，也最无实质内容的。<br />
<br />
虽然全国人大被标榜为中国最高权力机构，但从委员长吴邦国昨天所作报告中多番提到的落实中共各项方针路线，「确保如期实现党中央提出的立法目标」等词句来看，这一最高权力是何等地具有中国特色，就不言而喻了。<br />
<br />
机构形同「摆设」 报告内容空泛<br />
<br />
中共建政之初，并无人大这一机构，而是以全国政协「暂代」议会。直到1955年才召开首届人大，但人大一直被视为「摆设」，甚至连橡皮图章都不如，以至从三届人大到四届人大，中间隔了十多年人大竟未开过一次会议，形同被废。文革结束后，中共虽然开始重视人大这一机构，但只是作为一个官员退休的酬庸职位，人大委员长彭真、万里虽地位尊崇，但都不是中共政治局常委。直到1993年乔石接掌人大后，人大的实质地位才有所提升，全国人大的工作人员中至今提起当年「乔老爷」对人大地位的提升仍津津乐道。乔石的继任人李鹏虽然在总理任上因「六四」官声不佳，但其在人大委员长任内，对提升人大地位也不无贡献，起码，人大委员长在中共党内的第二号地位，是由其奠定的。<br />
<br />
吴邦国昨天的人大常委会报告，也不是全无内容，其中透露了一些信息，如今年要起草制定的新法例，包括行政强制法、增值税法和车船税法以及人民调解法、违法行为教育矫治法、涉外民事关系法律适用法、自然遗产保护法、基本医疗卫生保健法、社会救助法等。要修订的法律包括代表法、村民委员会组织法、行政监察法、预算法、土地管理法、森林法、水土保持法、职业病防治法、预备役军官法等法律。<br />
<br />
坚拒西方干预 异于政府报告<br />
<br />
而今年上半年，为配合编制「十二五」规划，人大将就国民收入分配、经济结构调整等多个课题，开展专题调研，向中央提出意见建议，也为明年人大会议审查批准「十二五」规划纲要做准备。<br />
<br />
吴邦国在报告中特别申明，「人大依法按程序集体行使监督职权，不代行行政权、审判权、检察权，不直接处理具体问题。」但在报告中反复出现多次的「坚持正确政治方向」可谓颇具特色，特别强调「深刻认识我国人民代表大会制度与西方资本主义国家政体的本质区别」，在与西方国家议会交往部分，亦重申反对「以言论自由和宗教信仰作幌子支持或变相支持『台独』、『藏独』、『东突』」。其意识形态色彩与强调「和谐世界」的政府工作报告，似乎是大异其趣。<br />
<br />
孙嘉业</div>

							
							
							
							
