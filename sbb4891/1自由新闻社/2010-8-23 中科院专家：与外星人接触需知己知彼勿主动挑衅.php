																											<div id="postmessage_793756" class="t_msgfont">中科院专家：外星人未必带来灾难 他们也有弱点<br />
<br />
<a href="http://society.people.com.cn/GB/12509172.html" target="_blank">http://society.people.com.cn/GB/12509172.html</a><br />
<br />
&nbsp; &nbsp;中新网广州8月22日电(杨薇)中国科学院紫金山天文台研究员、行星天文学家王思潮教授今日在广州出席“小谷围科学讲坛”时表示，他相信外星人的存在，而且外星人研发的飞行器有访问地球的能力。同时，他也不赞同英国科学家霍金的与外星人相遇对人类意味着灾难一说。 <br />
<br />
　　据悉，今年7月7日，杭州萧山国际机场由于不明飞行物的出现而被迫停止航班飞行，总共有18架航班受到此次事件的影响。此事也在海内外引起较大反响，激起了更多人对这些来历不明的飞行物的身份进行猜测。随后，中国坊间短时间频频听到有人发现不明飞行物的声音。<br />
<br />
　　关于这些不速之客，一直在海内外争议不断，有人觉得是眼睛的幻觉，也有人认为是一种特殊的气象。香港天文学会前会长陈铸略在接受凤凰卫视采访时表示，杭州出现的不明飞行物最可能是火箭碎片。<br />
<br />
　　但王思潮今日在做主题为“中国UFO真相”演讲时指出，火箭碎片的可能性可以排除。“火箭碎片陨落将在几十公里高的大气层产生耀眼的火球(火流星)，目击范围高达几万平方公里。与这次萧山不明飞行物特征并不相同。”他在经过一番调查得出，萧山不明飞行物飞行高度约1公里，有两种可能，在特殊情况下不期而遇的国内非普通飞行器(包括非普通飞机)；或是在低空快速飞行且无声或低声的特殊飞行器。<br />
<br />
　　对于这一说法，许多天文学家提出质疑，认为外星人没有研发出访问地球飞行器能力。其原因是太阳系外的恒星及其行星太远，航行要数十万年甚至数百万年，同时还要确保宇航员在长期飞行途中的生命安全，并解决食品、氧、水的供应和医疗服务。<br />
<br />
　　王思潮教授却提出自己的观点，他表示，“外星飞行器有可能比我们人类现代技术高得多，有可能达到0.8倍光速，这样就可能在不太长时间内完成往返。”他认为，用高智能机器人代替智慧生命进行星际航行，大大减少宇航员生命维持系统的困难。因而上述说法也能得到解决<br />
&nbsp; &nbsp; 对此，王思潮根据自己从事39年的天文观测，找到了事实依据。他发现离地面130公里至1500公里的近地空间，多次出现奇异的飞行物，其飞行速度远低于第一宇宙速度，有的低至每秒0.29公里，且在1460公里的高度平行地面飞行25分钟之久。“这说明飞行物有可能是具有抗地球引力能力的外星飞行器。否则，它们早就陨落下来。”王思潮阐述了自己的观点，他表示，自己运用了球面天文和物理的方法，并利用通过在紫金山天文台的长期观察，对一些重大的UFO事件进行定量分析，得出了以上结论。 <br />
 <br />
　　假如真的存在外星人，他们对地球是福还是祸？很多人担心外星人会有入侵地球的一天，英国著名科学家霍金曾表示，如果和外星人相遇，对我们人类意味着灾难。但王思潮认为，“要下这个结论，还为时太早。”<br />
<br />
　　面对众多听众的担忧，王思潮却显得十分乐观，他表示，霍金的观点是从美洲的印第安人得出的结论，但现在人类已认识并采取实际行动保护生物多样性。而且外星智慧生命的科技可能要比人类发达得多，他们可用科技解决能源，例如用核聚变能源，可到无生命的星球采矿或自己创造新材料。 <br />
<br />
　　“如果外星人对人类是善，就可以通过交流合作，促进人类文明的飞跃；如果外星人对人类的态度是恶，只要我们事前有准备，就能针对他们的弱点给以反击，毕竟他们也是生命，也会露出破绽。”王思潮觉得，或许外星人科技比地球人先进，想了解地球有无智慧生物，并不困难。但人类对外星智能生命进行直接接触要“应当谨慎，不要主动挑衅”，要懂得 “知己知彼”，才能“百战不殆”。</div>

							
							
							
							
