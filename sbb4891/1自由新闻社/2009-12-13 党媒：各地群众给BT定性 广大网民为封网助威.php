													<h2>中国清理关闭BT网站引关注</h2>
						
						
																											<div id="postmessage_370359" class="t_msgfont"><img src="http://i892.photobucket.com/albums/ac121/wodetuchuang/00-1.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
中国 更新时间 2009年 12月 12日 星期六 03:09 PM 格林威治标准时间<br />
<br />
记者: 陈苏 | 华盛顿<br />
<br />
　　中国最近关闭一些影视视听下载网站，这是当局过去一年来网络扫黄打非行动的一部分。观察人士认为，中国政府在必须采取行动打击盗版的同时，如何维护网络信息流通自由是个棘手的问题。<br />
<br />
　　中国当局对互联网的清理整顿升级，已经关闭了530多家视听节目服务网站，其中包括中国三大BT网站--BT中国联盟、伊甸园和悠悠鸟等为网民提供网络影视下载的知名网站。<br />
<br />
*政府：有利于网络影视行业的发展*<br />
<br />
　　中国媒体日前引述中国广电总局网络视听节目管理司的曹云霞的话说，这主要是“因为许多网站大量传播盗版节目，而且屡禁不止，会严重影响整个行业的发展”。曹云霞强调，今后所有从事互联网视听的机构都要获得许可证。<br />
<br />
　　包括“BT中国联盟”网站在内的几家知名BT类网站因为“无证上岗”而被关停引起中国网民的反响，担心其他没有获得广电局发放的“信息网络传播视听节目许可证”的影视视听网站将很快“被迫下岗”，其中包括可以下载美剧和影片的VeryCD、影视帝国、猪猪乐园、电影天堂等两百多家被广电总局视为非法的视听服务网站。<br />
<br />
*“电驴”突然不拉磨引发恐慌*<br />
<br />
　　被有些人称为“电驴”的VeryCD网站日前突然无法打开，网站上登刊出公告：“非常抱歉，VeryCD 服务器所在机房线路大面积故障”。虽然公告预计在12月10日中午恢复开通，但仍引起网民的恐慌，担心电驴步“BT中国”的后尘，被广电总局关闭，从此无法免费下载外国电影、美剧和音乐。有人估计，由于连日来网友担心月底前“电驴”被封，开始了“最后的疯狂”，连日来拼命下载，导致VeryCD网站瘫痪。<br />
<br />
　　中国业内人士认为，随着版权方权益意识的苏醒和政府监管的日益严格，影视分享下载模式恐怕已经走到尽头。<br />
<br />
*观察人士：中国监管当局是盗版猖獗的源头*<br />
<br />
　　但另一方面，批评人士在肯定中国打击网络盗版的同时也指出当局控制信息自由流通的问题。<br />
<br />
　　四川网络作家冉云飞说，中国盗版如此猖獗的源头正是由于当局对信息流通的严格控制。<br />
<br />
　　他说，中国当局如果真的希望保护知识产权，就让美国的信息、出版物和影视作品自由流通进入中国，而不是经某些监管当局的筛选。<br />
<br />
　　冉云飞说：“我们看的很多美国电影都是盗版碟，我们愿意看正版，但是你能让它正常流通进来吗？如果他们真正要强调中国对知识产权的保护，那么中国有关方面要按照WTO的协议，来进一步落实和实施言论、新闻和出版自由，同时使出版的书籍、影碟能够在中国自由流通起来。”<br />
<br />
　　杭州作家温克坚说，当局保护知识产权有合理之处。他说，网络上的信息自由流通、网友之间的信息共享、网上知识产权的保护与政府采取的措施等确实是非常棘手问题。但由于中国政府对网络一贯的严密监管控制，让网民产生极大的逆反，认定政府以扫黄打非、保护知识产权为名，钳制网上言论自由、信息自由。<br />
<br />
*局势与网络控制的关系*<br />
<br />
　　居住在深圳的作家赵达功说，网络作为新型媒体是信息传播的利器，twitter 和facebook一类网站的不断出现让中国当局感到恐慌。中国网民组成MSN 和SKYPE一类的网上聊天群体，令当局更难控制网上信息的传递。<br />
<br />
　　赵达功认为，目前的局势也让当局格外紧张：“目前来看，零八宪章一周年，刘晓波被捕一周年和世界人权日的到来，政治性话题就更多，议论就更多。所以他们千方百计就要控制、切断、封锁，干脆关闭很多包括BT这样的网站。”<br />
<br />
*广电总局的动机遭置疑*<br />
<br />
　　导致“BT中国联盟”等网站被关停的主要原因据信是它们没有办理广电总局的许可证。但冉云飞认为，不排除广电总局动机不纯，有垄断之嫌。他说，这可以被视为是广电总局与中国工业信息化部之间的争利行为。<br />
<br />
　　与此同时，杭州作家温克坚置疑中国广电总局等行政监管部门对互联网实行监管的资格。他说，除了广电总局之外，中国有大大小小上百个机构在对互联网行使监管权力，随意扼杀网民的表达权、知情权。<br />
<br />
　　他说：“在中国当下的行政架构当中，有几十个、上百个官僚机构都可以对网络实行非常随意化的管理和控制，这样的话，网民的权利和自由的确被他们搞得支离破碎了。”<br />
<br />
*网民的幽默--追悼被关网站*<br />
<br />
　　温克坚说，中国网民面对深受他们喜爱的多家网站被关停的局面，采取了各种嘻笑怒骂的幽默方式，包括给被关停的网站办丧事、写悼文的方式来表示他们的抗议。他说，他知道很多网友实际上非常愤怒和沮丧，认为他们在网上的一些权利突然被剥夺、被践踏了。<br />
<br />
　　据一份网络调查显示，中国网民对BT类网站被关闭大多数持反对立场，在对15696人的调查中，只有4．35%的人支持，95．65%的人反对。网民说，只为囊中羞涩，才不能去电影院看美国大片。再对BT影视下载网站这样封杀下去，“电影和音乐真就成了有钱人的享受了”。<br />
<br />
关键词：BT中国，版权，影视下载，电驴，VeryCD</div>

							
							
							
							
