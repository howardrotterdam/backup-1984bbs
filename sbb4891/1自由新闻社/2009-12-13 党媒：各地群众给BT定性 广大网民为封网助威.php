													<h2>做人不要太“BT”，广电总局关停非法BT网站大快人心</h2>
						
						
																											<div id="postmessage_371104" class="t_msgfont">2009年12月13日 14:58:46&nbsp; &nbsp;华龙网<br />
据新华网北京12月12日电，广电总局近日持续清理整顿包括BT网站在内的非法视听节目网站，目前已关闭BT网站530多家，其中包括“BT中国”等几家大型BT网站。广电总局有关负责人还表示，整治互联网视听服务是一项长期工作，只要被关停网站不解决侵权盗版、传播低俗色情节目等问题，就不可能重新开放。<br />
<br />
<br />
　　“BT”，在这里指的是一种视听节目下载方式，BT网站则是提供视听节目下载的网站。在网络语言中，“BT”则是另外一种意思，因为它是“变态”的拼音缩写，所以通常被网民用来骂人。不可否认，因为“BT中国”等视听服务网站的存在，给网民的文化生活的确提供了丰富与便利的条件，花微乎其微的代价，就可以轻松享受到影视大片带来的视觉和听觉冲击。然而，天下没有免费的午餐。当我们通过BT网站轻松享用一道道视听大餐时，却不可避免的遭受到盗版节目，以及夹杂的色情低俗内容的侵害。<br />
<br />
<img src="http://i45.tinypic.com/29uoeiv.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　众所周知，与欧美等文化产业发达国家相比，我国互联网影视节目版权保护工作远远落后。在2009年世界电影联盟年会上，一个来自欧洲的电影人公然宣称：“中国的盗版市场终将毁掉这个国家的电影产业”。虽然这多少有些危言耸听，却也是一记当头棒喝。事实上“BT中国”等视听节目网站公开、大量地侵权传播盗版视听节目，对节目版权所有者的知识产权造成了严重伤害。如此持续发展下去，毁掉的何止是中国的电影产业，甚至对中国网络文化产业的发展都将产生难以估量的损失。<br />
<br />
<br />
<br />
　　与此同时，由于“BT中国”等网站所传播地是侵权盗版的视听节目，而盗版生产者往往只顾追求经济利益，而忽略了社会责任，让正版的视听节目失去了市场，甚至为了满足少数人的特殊需要，刻意的进行人为再加工。导致所传播的视听节目良莠不齐，包含了大量的色情低俗内容，严重败坏社会风气，污染社会环境，危害未成年人的身心健康。如此一来，让广大忠实的拥趸者对BT网站是“爱在心口难开”，不用吧，难舍其方便快捷；使用吧，又难免遭受视觉和听觉污染。与其两难取舍，不如一关了之，广电总局此举也算为犹豫不决的网民解了一道难题。<br />
<br />
<br />
<br />
　　俗话说，不依规矩，不成方圆。现在是法治社会，人类的任何社会活动，都必须取得相应许可，就如驾驶机动车辆要拥有驾驶证、经商开公司要取得许可证、教师教书育人要持有从业资格证一样，从事互联网视听节目传播也必须取得有关主管部门的许可。早在2007年11月，广电总局与原信产部就联合发布了《互联网视听节目服务管理规定》，明确规定从事影视剧等视听节目下载业务的网站必须取得广电总局颁发的《信息网络传播视听节目许可证》。<br />
<br />
<br />
<br />
　　然而，此次广电总局本关闭的BT网站并没有取得许可证，根本就不具备从事互联网视听节目服务的资质。被关停的BT网站所传播的视听节目不但涉及侵权盗版，还含有大量色情低俗内容，根据《互联网视听节目服务管理规定》，它们也根本就不可能取得许可证。换一句话说，BT用户从这些被关停的BT网站下载的视听节目，其实就是“三无”产品，无正规产家、无质量保证、无正当渠道，而BT用户既是这个“三无”产品的受害者，同时又是这个“三无”产品“畅销”的帮凶。<br />
<br />
<br />
<br />
　　由此看来，为了获得经济利益，居然让“三无”产品大肆流向市场，这些被关停的BT网站还真是“BT”，恶有恶报，善有善报，被关停完全是在法理之中。我国有一句老话，“除了张屠户，不吃带毛猪”。热衷于在互联网上欣赏视听节目的网民，大可不必为关停了“BT中国”等非法BT视听节目网站而若有所失，能够为我们提供精彩视听节目的合法BT网站还大有所在，并且可以让我们远离盗版、远离色情、远离低俗。<br />
<br />
