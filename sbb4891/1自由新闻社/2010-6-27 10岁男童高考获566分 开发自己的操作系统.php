新闻来源:《济南时报》<br />
尽管被人称作小“神童”，可10岁的苏刘溢完全不管这些。 拍照时，他会恶作剧地用手捂住脸；大人说话时，他会突然发出一声怪叫吸引注意力；甚至在面对着名牌大学的招生面试时，他依旧坐不住，在考场里跑来跑去。作 为今年全国年龄最小的高考生，理科生苏刘溢考了566分。尽管距离一本线还有段距离，但他却期盼着能到一流大学里学习更多新知识，期盼着能有名牌大学对他 破格录取。在本报记者的联系下，山大招办昨天与苏刘溢碰面，但是否能破格录取仍很难说。<br />
<br />
<img src="http://img.cnbeta.com/newsimg/100627/13144201440353870.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
10岁“神童”苏刘溢在接受山东 大学招生办公室人员的面试<br />
<br />
<br />
10岁男孩8岁上高中，想到山大学数学<br />
<br />
26日一早6点多，泰安的刘欣欣就睡不着了。母子俩要来济南参加本报等举办的高招会。<br />
<br />
刘欣欣的儿子叫苏刘溢，10岁，人称泰安小“神童”。2000年苏刘溢出生，因对幼儿园生活不感兴趣，父母将苏刘溢接回家中自学。小学课程 在他入学前已学会。<br />
<br />
2007年，7岁的苏刘溢在泰山博文中学读初中，一年内结束初中 全部课程。2008年9月，8岁的苏刘溢进入泰安二中高一试读，但读了不到一年退学在家自学。他在Windows帮助文件下学会编程，在一年多时间里，又渐渐学会C语言、VB等十余种语言编程。他还开发了自己的操作系统，内含编程软件、Office、火狐等程序。<br />
<br />
2010年6月，10岁的苏刘溢首次参加高考，高考成绩总分566分。其中，数学130分，理综 181分，语文88分，英语118分，基本能力测试49分。<br />
<br />
566分这个分数上一所 普通本科二批院校问题应该不大，但刘欣欣告诉记者，儿子很聪明，喜欢学习和研究新知识，接受能力特别快。她和儿子都将目标锁定在了一本线上的名牌学校。<br />
<br />
苏刘溢比较喜欢数学和电脑，早已对山大数学专业心仪已久，但总分566分仍没达到一本线，按高招 正常程序被山大录取似乎存在困难。“孩子完全具备接受大学知识的能力，我们真的很希望孩子能到更好的平台去接受教育。”刘欣欣说。在本报记者等人的联系 下，山大决定与苏刘溢碰面，当面了解10岁高考生。<br />
<br />
“小不点”对话 山大教务处处长反应快、思维活跃获赞赏<br />
<br />
昨天上午11点，苏刘溢和 妈妈下了火车，立刻打车赶到本报等举办的2010年高招咨询会现场，直奔山大展台。山大教务处处长王仁卿和招办副主任朱德建已在会场等待着苏刘溢。<br />
<br />
“Howareyou(你好)。”刚一见面，王仁卿就用英语和小家伙打着招呼。<br />
<br />
苏刘溢丝毫没有怯场，自信地用英语回答着：“Fine，thankyou。”<br />
<br />
看到咨询会现场来了个“小不点”，不少前来咨询的考生和家长都好奇地围了上来。听说这个10岁男孩 也是今年的高考生，现场的考生和家长们都露出惊诧表情。<br />
<br />
在主办方为双方提供的会议室 里，王仁卿和朱德建一起与苏刘溢进行交流。说起自己的高考成绩，苏刘溢认为“考得一般”，“尤其理综没有发挥出自己的水平。”<br />
<br />
王仁卿问苏刘溢平时喜欢干啥，小家伙回答喜欢看书，不过以前喜欢看趣味性的书，最近喜欢看大学语 文、大学数学等。刘欣欣在一旁补充着：“孟子、论语等书籍，孩子都已经看过了。”<br />
<br />
妈 妈和招办老师正说着话，苏刘溢已悄悄地离开座位，被会议室里的一个扩音器吸引了。摆弄了一会儿，他回来告诉大家他弄明白了扩音器的发音原理。“从泰山山下 到山顶为啥气温会有差别？”听着王仁卿的问题，苏刘溢称用物理的知识来解答，讲起了气压等。他还根据泰山高度等算出了山下山顶的温差是几度。“这孩子反应 很快，思维也很活跃。”朱德建称赞苏刘溢。<br />
<br />
关于生活自理：会洗衣 服，最近在跟妈妈学炒菜<br />
<br />
“为啥这么小想上大学？”王仁卿问苏刘 溢。<br />
<br />
苏刘溢想也没想就回答，因为想学习更多新知识，所以要进入大学。因为进入大学必须要考试，他就参加了高考。<br />
<br />
啥叫好大学？对于这个问题，苏刘溢有自己的理解。他觉得 “好大学不是用一本二本来衡量的，更关键的是好大学要有好的师资力量，给学生提供好的平台”。听着苏刘溢小大人般的回答，在座的人都笑起来。苏刘溢伸出手 冲大家摆出成功的手势，口里喊着“耶！”这时，他和自己的同龄人一样，调皮可爱。<br />
<br />
会 不会洗衣服？会不会做饭？王仁卿和小家伙聊起了生活自理。苏刘溢说自己会洗衣服，但没洗过袜子，平时更多的是妈妈代劳。苏刘溢喜欢吃西红柿炒鸡蛋，所以最 近正在积极跟妈妈学炒菜。<br />
<br />
因为年龄小，苏刘溢对周围的东西都充满好奇，在椅子上坐不 了一会儿就开始走动。王仁卿问他如果上大学是不是能管住自己？上课时候能坚持坐多长时间？苏刘溢回答自己没问题。他同时还表示了对自己的“敬佩”―――他 觉得自己坐下来坚持高考就是个奇迹，尤其考试前半小时对他来说是“干熬了半小时”。在高考期间，苏刘溢几乎每科都提前近半个小时交卷。<br />
<br />
山大招办“感觉还不错”能否破格录取还不好说<br />
<br />
对于苏刘溢，王仁卿和朱德建感觉还不错，但最后结果如何还不好说。因为高校破格录取并不是一件容易 事儿。<br />
<br />
山大招办副主任朱德建告诉记者，山大一直在探索特殊类型人才的招生和培养，以 前也曾有破格录取的例子。但考生是否能破格录取，最后还要由教育部门定。<br />
<br />
另外，为保 证招录公平性，破格录取有着一套严格的程序。这次见到苏刘溢后，山大招办人员将回学校进行汇报，看是否能破格录取。如果有此意向，苏刘溢还要接受专家委员 会一系列考核，然后报教育部批准。<br />
<br />
“2005年，我们学校曾招收过一名14岁的高考 生，目前已毕业到美国攻读硕博连读。”朱德建说。据介绍，这名叫张凯笛的女孩来自平邑一中，2005年以651分考入山大生命科学学院。和苏刘溢不同，张 凯笛一直从学校读到高中，但一路跳级成为“低龄高考生”。<br />
<br />
万一不被山大破格录取咋 办？一家人很茫然，决定先报上二本志愿<br />
<br />
“太累了”，10岁“神童” 拒绝再高考<br />
<br />
