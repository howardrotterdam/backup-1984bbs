2、“中国一些垄断行业贪得无厌”：　中国目前一些垄断行业还没有贪得无厌够,还需要人民多出血肉,因此,不要再想降价的事了.<br />
<br />
点评：此帖纯粹是发泄对社会的不满情绪，作为评论员，应该正面引导网民进行理性思考，正确认识改革进程中存在的问题。<br />
<br />
3、“湖南长沙出现辐射雾天气 污染是主要成因”：　长沙的空气污染前些年可是有些出名的,近几年经过努力，好了许多，现在有些问题回潮，希望政府加大治理力度，无愧于长沙是最适宜于人居住的城市之一的称号，展现给大家一个更美丽的山水洲城。<br />
<br />
“干部观察员是嘛玩艺”：　成立观察员好像是用心良苦，但并不是真正的创新，创造的只是更多的花招，请别创造新的失望！<br />
<br />
“政绩工程危害大”：希望早日建设成为法制社会。<br />
<br />
“法律是严肃的权威性的”：　既然立有法,为什么又不执行?讲客观原因,法律是不允许的.<br />
<br />
点评：虽然是提意见、建议、希望，但仍然只是不满情绪的表达，作为评论员，应该要说清事情的原因，引导网民正确分析，这样才有利于社会的和谐、稳定。<br />
<br />
4、“狗日的真心人”：　严厉抨击真心人,你这个狗东西是在宣扬日本军国主义的战功,我们中华民族的伤痕已经很深,谁要你这种数典忘宗的东西拿民族的耻辱作为自己的玩好.<br />
<br />
点评：应该理性引导，有话好好说，不能用漫骂的方式。<br />
