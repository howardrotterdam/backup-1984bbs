点评：正视现实，又鼓舞人心，激人奋进，这样解读报告，比那些单纯叫好的帖子强多了。<br />
<br />
2、“交警查县委书记座驾遭围殴”：　”我们不怕网络上有各种说法”.这句话出自县委书记刘桦之口,真有点不可思议.过去把县官叫成父母官,现在是人民的公仆,党的干部,更应尊重人民,敬重人民,要常怀畏民之心,才能体会权力来自于人民,实践权为民所用.如果事实真相如报道所述,那么这位县委书记真应反思.好在此事已经引起湖南省委领导的重视,相信此事会得到一个公正满意的处理.<br />
<br />
点评：这样的事，确实不好评论，稍有不慎，就可能招致网民的攻击。本帖换了一个角度，其落脚点是给人希望，给人信心，这样，就起到了正面引导的作用！<br />
<br />
3、“农大拆迁事件另有隐情”：　一所大学敢如此蛮横吗？据知情者透露，此事严重失实，事实真相因私人的原因被夸大报道了。据说是某刊子女在该校舞弊被抓。<br />
<br />
点评：揭露不实报道的内幕，减少不良影响。<br />
<br />
4、“各地为什么千方百计禁止有人到京上访”：　国家那么大，人口那么多，在经济社会全面改革和转型时期，局部出现一些腐败现象完全是可能的，也不足为怪。有问题并不可怕，可怕的是视而不见。值得庆幸的是，我们党和政府长期重视反腐败问题，并把它提到关系到党和国家的生死存亡的战略高度，提出要通过制度建设来遏止腐败。我们要有足够的信心相信党和国家能够处理好这个问题，社会公众也应该在信任的基础上按照正当合法的程序和渠道反映情况。当前，党和政府反腐败重点应该放在解决那些群众最关心、与群众利益关系最密切的领域和问题上，增强社会公众对反腐败的信心和支持。同时，在反腐败的过程中，我们必须时刻提醒自己，一定要珍惜现有的社会和经济环境，因为经济的快速发展、人民总体生活水平的不断提高以及社会公众对党和政府的信任和理解，才使我们能够在一个宽松的环境中解决腐败问题，为构建社会主义和谐社会提供有力的保障。<br />
<br />
点评：本帖引导网民冷静思考社会问题，提出了一些理性的观点，非常不错。一是“局部出现一些腐败现象完全是可能的”，二是“有问题并不可怕，可怕的是视而不见”，三是“值得庆幸的是，我们党和政府长期重视反腐败问题，并把它提到关系到党和国家的生死存亡的战略高度”，四是“一定要珍惜现有的社会和经济环境”。<br />
<br />
5、“漂亮的女大学生去学足浴工，到底便宜了谁？”：　大学生做什么都可以啊，为什么做足浴就要不得，楼主分明就是歧视这些做足浴的嘛。大学生这么多，每个人都一定找到适合的工作？做足浴的这么多，就没有一个素质比得上大学生的？大学生做足浴也不是一件普遍的事，不要把个别现象看成是普遍现象，把责任全部推给政府。大学生现在就业确实有困难，但不能因此宣扬读书无用论吧，而且读了大学也只是有了一个就业的敲门砖，并不等于就一定得给你一个好位置吧。<br />
<br />
点评：本帖针对就业难这个热点问题，提出了很好的观点，一是不能搞行业歧视，二是“大学生做什么都可以，读了大学也只是有了一个就业的敲门砖，并不等于就一定得给你一个好位置”，三是“不要把责任全部推给政府”。这些观点，对消除民众在就业问题上的思维误区和对政府的不满，有积极意义。<br />
<br />
二、不合格帖及点评<br />
<br />
1、“交警查县委书记座驾遭围殴”：真为这个县委书记的素质惊讶!这哪里是个县委书记,分明是个土皇帝山大王黑社会老大!建议不但要严查这次袭警事件,还要深入调查这个人!这样的人,怎么能为一县父母之官!<br />
<br />
点评：网上这条消息谁是谁非还没有调查定论，如果评论员发帖文纯粹批评县委书记，只会强化负面影响，增加网民的不满情绪，降低党和政府威信，与“ 正面引导”的原则背道而驰了。还有的帖子，将此话题引向对特权的批评，有的引向个别交警不文明执法的现象，越扯越宽，更加不利于社会的稳定、和谐。<br />
