《党校阵地》网评员管理办法<br />
 <br />
 <br />
 <br />
2010-1-8 8:43:42 访问量：6456 <br />
 <br />
<br />
--------------------------------------------------------------------------------<br />
 <br />
　 <br />
&nbsp;&nbsp;<br />
<br />
第一章 总&nbsp;&nbsp;则<br />
<br />
第一条 为加强党校阵地网评员队伍建设，规范网评信息发布，积极引导网上舆论，使党校阵地网页更好地服务党校工作，特制订本办法。<br />
<br />
第二条 本办法适用于党校阵地网评员。<br />
<br />
第三条 党校阵地网评员由衡阳市委党校办公室实行统一认定与管理。<br />
<br />
第二章 网评员条件<br />
<br />
第四条 政治立场坚定，拥护中国共产党领导，坚持党的路线、方针、政策，遵纪守法,有良好的思想品德和敬业精神。<br />
<br />
第五条 有一定的理论基础，善于运用网络语言，知识面广，有较强的文字功底。<br />
<br />
第六条 了解党校工作，具备一定的电脑基础知识，能够熟练运用相关网络应用软件。<br />
<br />
第七条 接受党校阵地网页业务指导。<br />
<br />
第三章 网评员职责<br />
<br />
第八条 在党校阵地网页发表评论、跟帖或原创评论文章，特别是对党校动态和大家关注的热点、难点、焦点问题，要主动发表评论，客观公正评述，积极引导舆论。<br />
<br />
第五章 网评员管理与奖励<br />
<br />
第十五条 通过网评员管理系统，或以QQ群、座谈会、交流会等形式，加强网评员业务学习与培训。<br />
<br />
第十六条 网评员在党校阵地上发表的评论按月进行统计。统计结果在网页公告栏里公布。<br />
<br />
<strong>第十七条 网评员补贴。评论奖励0.1元/篇，每月奖励不超过100元，每半年计发一次。</strong><br />
<br />
第六章&nbsp;&nbsp;信息安全<br />
<br />
第二十条 网评员不得制作、复制、发布、传播含有下列内容的信息：<br />
<br />
（一）反对宪法所确定的基本原则的；<br />
<br />
（二）危害国家安全，泄露国家秘密，颠覆国家政权，破坏国家统一的；<br />
<br />
（三）损害国家荣誉和利益的；<br />
<br />
（四）煽动民族仇恨、民族歧视，破坏民族团结的；<br />
<br />
（五）破坏国家宗教政策，宣扬邪教和封建迷信的；<br />
<br />
（六）散布谣言，扰乱社会秩序，破坏社会稳定的；<br />
<br />
（七）散布淫秽、色情、赌博、暴力、凶杀、恐怖或者教唆犯罪的；<br />
<br />
（八）侮辱或者诽谤他人，侵害他人合法权益的；<br />
<br />
（九）含有法律、行政法规禁止的其他内容的。<br />
<br />
一旦发生并造成不良后果的，市委党校将按相关规定移送司法机关，追究有关人员的责任。<br />
<br />
第七章 附&nbsp;&nbsp;则<br />
<br />
第二十一条 本办法由中共衡阳市委党校负责解释。<br />
<br />
第二十二条 本办法不对外公布，仅限于网评员掌握。<br />
<br />
第二十三条 本办法自即日起实施。<br />
