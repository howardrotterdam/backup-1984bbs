大隐 2008-09-11 18:05:36 回复<br />
<br />
一忌导向问题。<br />
<br />
1、只是普通的网上发言，没有正面引导意义。<br />
2、批评政府、公共事业单位。不论是直接批评政府，还是向政府提意见、希望，在网民中产生的影响，都是“这个政府不好”，不仅没有正面引导意义，而且还会起负面影响。　　&nbsp;&nbsp;3、对国际或外交事务作不恰当的评论。<br />
提醒：<br />
1、贴网评之前，请明白“网评员”的定位。直白地说，网评员是“舆论引导员”，应该进行正面引导。网评员的主要职责是：维护党和政府及衡阳的形象，维护社会稳定，促进社会和谐。<br />
2、谈观点、分析问题的，不一定是网评，必须有正面引导意义。<br />
3、如果罗列了一大堆不好现象后，简单加句正面的话，其效果仍然是负面的。<br />
<br />
二忌帖子凑数。<br />
1、复制或摘录新闻作主帖。网评应该是自己写的。你可能与其他网民的观点相同，但要用自己的话表达出来。<br />
2、理论文章、工作总结作主帖。很难有引导效果。<br />
3、有的跟帖只是重复新闻中的内容。对网民没有引导效果。<br />
4、有的只是针对一些生活琐事。如谈饮食、身体保健等等，不符合网评的内容要求。<br />
提醒：以上情况，一律不计算工作量。<br />
<br />
三忌官方语气。<br />
1、用作报告的语言，讲大道理。很难有引导效果。<br />
2、帖子中夹杂官方语言。比如，“我们的政府”、“在衡阳市委市政府的领导下，一定有美好的明天”。网民一看就知道是“政府的人”，起不到引导效果。<br />
提醒：网络有网络的规律，网评，是写给网民看的，应该以普通网民的语气来写，要网民喜闻乐见，不要让人产生反感。<br />
<br />
几点注意事项：<br />
1、引导的重点是：“与衡阳有关、影响重大的亮点、热点、焦点、难点问题。”请多在这几个关键问题上下功夫，不要去评鸡毛蒜皮的事、哗众取宠的事。<br />
2、希望多看我们的通知、提示、要求。发现工作中的问题后，我们会提示，但可能有的同志没去看。<br />
3、请关注精华帖，以了解网评的方向。<br />
