<br />
　　本报讯 昨天下午，市委宣传部举行特约网评员聘请仪式。我市15名特约网络评论员接过聘书，正式上岗。<br />
<br />
　　随着互联网的快速发展和网民的剧增，网络已成为一种新兴的主流媒体。建设一支业务精、素质好的网评员队伍，开展有效的网络舆论引导是当前宣传思想工作的一项紧迫任务，具有十分重要的现实意义。据了解，特约网评员将就网民关注的热点、难点问题参与评论，解疑释惑，针对网民反映的问题，跟踪舆情处置情况，协调配合相关单位及时进行网络回复和反馈，确保正面向上的舆论导向，切实营造健康稳定和谐的网络环境。<br />
<br />
　　会议要求，特约网评员要充分认识网上评论工作的重要性和急迫性，进一步明确网上评论工作的指导思想和主要任务，形成合力共管、公开发布、舆情提示、培训交流相结合的工作机制，发扬奉献精神，树立求真意识，热心网评，尽心、贴心、用心地建设好和谐网络文化，为我市营造良好的网络舆论环境作出贡献。<br />
<br />
　　特约网评员纷纷表示，将切实履行好工作职责，不断提高业务水平，成为政府与网民沟通交流的桥梁。<br />
<br />
　　据悉，这15名特约网评员既有部分乡镇和部门工作人员，又有民间论坛版主和新闻网站代表，还有大学生村官，具有一定的广泛性和代表性。<br />
