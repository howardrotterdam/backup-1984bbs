													<h2>外交部驳“奥巴马就世界新闻自由日声明”</h2>
						
						
																											<div id="postmessage_604680" class="t_msgfont">外交部驳“奥巴马就世界新闻自由日声明”（附声明全文）<br />
<br />
<br />
<br />
　　外交部发言人姜瑜昨天就美方有关“世界新闻自由日”的声明答记者问。答问如下：<br />
<br />
<br />
　　问：美国总统奥巴马5月3日就“世界新闻自由日”发表书面声明，批评包括中国在内的一些国家限制新闻和言论自由。中方对此有何评论？<br />
<br />
<br />
　　答：改革开放以来，中国新闻事业蓬勃发展，新闻从业人员不断增加并依法享有充分的新闻报道自由，其各项权益得到依法保护。中国政府依法保护公民的言论自由，充分发挥新闻媒体和公民的舆论监督作用。同时，法律面前人人平等，任何人不得以任何借口违法犯罪。我们敦促美方尊重事实，正确看待中国的新闻自由状况，停止对中国的无端指责。（外交部网站）<br />
<br />
<br />
　　附：奥巴马总统就世界新闻自由日发表声明<br />
<br />
<br />
　　白宫于5月3日公布了奥巴马总统就世界新闻自由日（World Press Freedom Day）发表的声明。以下是声明全文，由美国国务院国际信息局（IIP）翻译。<br />
<br />
<br />
　　白宫<br />
<br />
<br />
　　新闻秘书办公室<br />
<br />
<br />
　　即时发布<br />
<br />
<br />
　　2010年5月3日<br />
<br />
<br />
　　奥巴马总统就世界新闻自由日发表声明<br />
<br />
<br />
　　每年的5月3日是世界新闻自由日，它提醒我们所有人谨记这一核心自由极其重要的意义。我们在这一天表彰新闻工作者所发挥的不可或缺的作用：他们反抗滥权，揭露腐败，让所有公民都了解影响我们的世界的重要议题。我们在这一天还要就媒体受到的限制以及许多新闻工作者及其家人因从事新闻工作受到威胁、暴力或监禁而发出警示。<br />
<br />
<br />
　　去年就世界新闻自由而言是情况恶劣的一年。在人们通过互联网、手机和其他形式的联网技术获得前所未有的信息的同时，中国、埃塞俄比亚、伊朗和委内瑞拉等国政府对充分获得和使用这些技术施加限制，以此遏制言论自由。<br />
<br />
<br />
　　此外，去年因从事本职工作而被杀害的新闻工作者比近年来任何一年都多。去年死亡人数多的原因主要是，除俄罗斯、索马里、墨西哥和洪都拉斯的新闻记者被杀害之外，在菲律宾马京达瑙省有30多名新闻记者被杀害，其原因与选举有关，是有史以来新闻记者死亡人数最多的一次事件。如往年一样，今年被杀害的新闻记者中有将近四分之三从事地方新闻报道，在自己的国家遭到杀害。<br />
<br />
<br />
　　昌西・贝利（Chauncey Bailey）正是这样一名地方记者。贝利是他所在的加利福尼亚州奥克兰市一名孜孜不倦的地方记者，他因揭露许多滥权和腐败行为而广受尊敬。三年前，他带一个无家可归的人去吃早餐途中在自己办公室附近被枪杀。对受到指控的凶手的庭审定于今年夏天开始。坚决追究罪责对于阻止这类攻击再次发生至关重要。但我不无忧虑地看到，在杀害新闻记者的凶手中，10人中几乎有9人得以逃脱罪责，因此我敦促各国政府对这个问题采取措施。<br />
<br />
<br />
　　世界各国有越来越多的新闻记者和博客作者遭到监禁。伊朗在上一届选举后对异议人士进行了镇压，现在被监禁的新闻记者多过其他任何国家。白俄罗斯、缅甸、中国、古巴、厄立特里亚、北韩、突尼斯、乌兹别克斯坦和委内瑞拉等国政府对那些撰文批评政府领导人及其政策的新闻记者实行了监禁。<br />
<br />
<br />
　　然而，每有一位受到攻击的新闻工作者，便有无数更多的人不顾遭到报复的危险继续为自己的社区提供消息。在世界新闻自由日这一天，我们表彰那些在诸多挑战和威胁面前奋不顾身从事这些重要工作的人，并弘扬一项将自由、独立的新闻媒体视为生机勃勃、充满效力的民主制度的核心的原则。（据《美国参考》整理）</div>

							
							
							
							
