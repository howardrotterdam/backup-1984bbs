任何研究中共的人都很快面临两个尖锐的问题。第一是为何一个党制造了这么多悲剧却仍然大权在握――20世纪60年代的文化革命中，1958―1960年历史上最严重的人为的饥荒导致三千五百至四千万人死亡，除了1989年天安门事件以外，还没有出现任何严重的威胁。二是为什么它仍然自称是“共产主义政党”，即使当今中国对任何有着平等梦想的人看来，似乎更接近于英国维多利亚时期残酷无情的资本主义。<br />
