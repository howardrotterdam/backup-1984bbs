通过故事和实例，《金融时报》的长期驻华记者马利德，阐明了最重要的矛盾和悖理。举其中一个显而易见的为例，市场经济的需要 vs.党的控制。马利德描述了一个几乎是滑稽的战场：中国国有控股公司在海外股票市场的上市。华尔街银行家曾绞尽脑汁地力图描述党委书记在企业中的角色。约翰・桑顿，高盛公司的前老板，描述在一个“大开眼界”讲座中，他作为中国的某董事会成员了解到：该委员会负责六大功能，“而且都是最重要的事。”招股说明往往通过避免提及党的角色来解决这个难题。<br />
<br />
这方面的矛盾一个更让人恶心的例子是在2008年，一家乳品公司，三鹿的部分产品已被发现受到污染，正在危害和导致儿童死亡。仅从商业逻辑来说，更遑论基本的人道，都要求这批产品被立即召回。但是老板首先要效忠于党，党要求控制坏消息，以免破坏当时北京奥运会的气氛。<br />
<br />
最生动地显示在1989年的北京，然后党只有依赖军队才保住了政权，接下来是因此而出现的紧张态势。这种依赖导致了迅速膨胀的军队预算，将军们用来配备高科技设备。这些让他们认为自己是作为职业军人在保卫中国，其实他们的工作是为党服务。偶而媒体上出现神秘的（非公开的）反对军队“去政治化”和“国家化”的呼声，这些论述也显示出局势的紧张。<br />
<br />
第三，还有一个悖论，中国的领导人认识到，对他们的权力主要的威胁是腐败，但他们的权力建立在让腐败变得基本上无法避免的体系之上。事实上，正如马德利所说，腐败已经成为一种“象交易税那样东西，让统治阶级在内部分配不义之财……这成为黏合整个系统的胶水。”外人不允许有超过党的权威。马德利注意到，独立的反腐败运动“可以带动整个大厦轰然倒塌。”<br />
<br />
这是作者所说的“基本悖论”的一部分：“一个强大，全能的党制造出了一个虚弱的政府和妥协的惯例。”这使得它在应对未来的变化的时候显得左支右绌：中国正在“重新平衡”其经济，刺激国内消费，提供一套全面的社会安全网和“从扭曲中继续获取既得利益”。马德利似乎认为党的历史表明，它会一如既往，找到方法，来完成下一次转变。但他也注意到，中国领导人最近几个月的胜利似乎比较“脆弱”。党的统治总是让它难于描绘出与现在非常不同的未来蓝图。但在中国，未来和现在往往天差地别。</div>

							
							
							
							
