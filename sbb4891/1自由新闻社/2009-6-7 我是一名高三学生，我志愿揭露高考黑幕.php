&nbsp; &nbsp;有人说，高考是最不公平的，也有人说，没了高考，才是最大的不公平。我个人感觉这个东西就和吴思老师所讲的血酬定律一样，总要从你身上拿掉这么多东西的。我家没有权，不能写白条往上递，送我读书。我家也没有财力，供我出国，那是一笔不小的费用。我家也不能使用公权力和共财力，先去非洲的某小国家取得国籍，然后回国以留学生的名义读高等学府。<br />
