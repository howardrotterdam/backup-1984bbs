&nbsp; &nbsp;在中国，真诚是个奢侈的品质。从没上学的时候开始，父母就告诉我，诚实，善良，见义勇为，善于待人。可有一天，自己出了某件事之后，父母好像就瞬间拍拍自己的肩膀，轻声的说：“这个世界不是这样的，你要学会……”<br />
