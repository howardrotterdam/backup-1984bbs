对于VISA是否真的会对违规的银行处以罚款，以及有法律界人士质疑VISA的罚款是否具有法理依据及可行性，刘婕表示，这牵涉到VISA与合作银行合作协议的具体条款规定，既然协议是双方认可签订的，双方都要遵守并执行协议。<br />
<br />
她的言下之意似乎暗示，VISA在与银行签署合作协议中，具有相关的违规处罚内容，VISA如果实施处罚，也只是“照章办事”。<br />
<br />
中国银行上海分行一位银行卡业务负责人称，此次VISA收紧的只是在中国境内发行的“4”字头的借记卡或者双币种信用卡。“4”字BIN号打头的卡尽管在当初发行时可能也同时打上了银联的标识，但知识产权是属于VISA，提供清算通道的权利也在VISA。当初之所以打上银联标识，只是为了方便卡在中国境内使用。VISA此次的行动基本上是“有理有据”。<br />
<br />
“另一方面讲，银联具有自主知识产权的‘62’打头的银联标准卡在境外如何清算，只要银联和全球各银行签订清算协议，VISA无法也无权干涉。问题其实就是出在近些年来国内大量发行的双币种借记卡和信用卡上。”<br />
<br />
银联称VISA无权单独对境外通道作出限制<br />
<br />
对于VISA这次最新的“攻势”，中国银联今天在发给网易财经的回应中表示，银联已经注意到近日媒体有关“银联、VISA”双标识卡问题的报道。该人士向网易财经表示，“银联、VISA”双标识卡不是纯粹的单品牌银行卡，标识双方均有责任和义务为持卡人提供境外受理服务，这也直接关系到持卡人的权益和发卡银行对持卡人的承诺等。任何一方都无权单独对持卡人选择境外支付通道做出限制，持卡人拥有选择境外支付通道的权利。<br />
<br />
该人士称，中国银联作为立足中国，走向世界的银行卡组织，担负着推动银行卡产业发展、满足广大持卡人支付需求的重要责任。在境内外成员机构的有力支持下，中国银联将不断扩大业务领域，提高服务水平，进一步加强国际合作，积极完善全球受理网络，与产业各方一道，努力为广大持卡人提供更加优质、安全、高效的支付服务。<br />
<br />
目前，银联国际受理网络已经延伸至境外90个国家和地区，能够基本满足中国人在经常到访的国家和地区进行商务、旅游、留学等支付需要。银联国际网络不仅可以受理单标识银联卡，也可以受理双标识银联卡。特别是62字头单标识银联卡目前已经在境内、外广泛发行和使用。在境外交易使用单标识银联卡，不仅可直接走银联网络，免收约占交易金额1％－2％的货币转换费，而且可以享受银联提供的多种增值服务。<br />
<br />
双币种卡“怪胎”<br />
<br />
多位银行卡业内人士向网易财经表示，双币种卡是中国银行卡发展的特殊历史造成的，背后是VISA等国际银行卡组织在中国市场的发展策略，以及与逐渐成长起来的中国银联的利益博弈。<br />
<br />
VISA等银行卡组织早在上世纪80年代就已经进入中国，但最早中国各大银行银行卡业务几乎为零，也没有跨行互通的网络，VISA在中国主要是做外卡进入中国使用的业务。后来，中国的银行开始大量发行银行卡后，银联当时并未成立，各银行纷纷采用了VISA“4”字头或者万事达卡“5”BIN号的卡片，VISA和万事达为了市场战略，在其中还给以中国的银行相当奖励。<br />
<br />
按照“谁的BIN号谁转接”的原则，中国“4，5”字头BIN号的银行卡理应由VISA或者万事达来提供跨行清算通道，并获得其中产生的收取于银行和持卡人的费用。但由于支付清算涉及国家金融安全，央行并未对外卡组织开放国内的支付清算市场。使得国际银行卡组织只能做外卡在中国境内使用，和中国的银行卡在海外使用的相关服务。<br />
<br />
中国银联成立后，几乎独家垄断了国内的跨行支付清算业务，基本上与外卡组织形成了中国银联享有国内市场，外卡组织享有境外市场的利益格局。在境内银行的信用卡业务开始迅速发展时，为了方便信用卡在境内外使用，各家银行都大量发行了既带有外卡标识又带有银联标识的双币种信用卡。由于国外信用卡业务消费是主流，随之给银行卡组织带来的利润也最多，所以，VISA和万事达都加大营销力度，以现金发卡奖励等方式鼓励国内银行大量发行外卡BIN号的双币种信用卡，虽然卡片上有银联标识，但主动权却在外卡组织手里。<br />
<br />
银联意识到BIN号标准的重要性后，也申请了具有自主知识产权“62”BIN号，发行银联标准卡，并且全力在在境内发行推广银联标准卡。银联要求境内银行新发行的借记卡必须采用银联标准卡，并且对原有外卡BIN号的借记卡进行替换。在信用卡上，也鼓励银行单独发行银联标准信用卡。为了遏制外卡组织在双币种信用卡上已经形成的竞争优势，2008年来一直有消息称，银联还在积极游说央行禁止银行发行同时带有银联和外卡标识的双币种信用卡。<br />
<br />
双标识信用卡或将退出历史舞台<br />
<br />
某国有银行银行卡中心人士称，银联一方面在境内扩大竞争优势，另一方面积极拓展海外网络。“外卡组织当然不能坐视银联在享受国内巨大市场的同时，又来争抢境外市场。此次VISA向会员银行施压后，很可能万事达等组织也会采取同样行动。”<br />
<br />
该人士分析认为，VISA此次行动另一个意图是，如果中国的主管部门出面进行调停，或许会加快央行放开国内支付清算市场，达到利益交换的目的，“中国开放境内的市场，外卡给以银联海外扩张提供方便。”<br />
<br />
多位业内人士称，此次如果VISA果真全面实施禁止会员银行为银联提供清算通道，银联和外卡组织的正面冲突在所难免。央行必须对目前还在发行双币种信用卡进行重新规范，双币卡可能将逐步退出历史舞台。<br />
<br />
民生银行上海分行卡中心一位人士甚至直言，双币种卡是一个“怪胎”，叫法也有问题。一个银行卡组织具有全球网络，可以在数十个不同币种的国家使用，毫无必要自称双币卡或者多币卡。<br />
<br />
网易财经致电另一家全球银行卡组织万事达卡，问及是否会跟进VISA的行动时，万事达卡上海代表处相关负责人称，“不便对此作出任何回应。”</div>

							
							
							
							
