													<h2>VISA组织发通知封堵中国银联境外通道</h2>
						
						
																											<div id="postmessage_653679" class="t_msgfont">国际卡组织VISA和中国银联的渠道争夺战升级。记者昨天从商业银行获悉，VISA国际组织近日向全球会员银行发函要求，从8月1日起，凡在中国大陆境外受理带VISA标志的双币种信用卡时，不论刷卡消费还是ATM取现，都不得走中国银联的清算通道，否则VISA将重罚收单银行。中国银联昨天未就此事做出回应。<br />
<br />
根据VISA的函件，如果收单银行走中国银联网络进行清算，第一次将罚款5万美元。如果收单行再犯，每月将罚款2.5万美元。VISA昨日回复称，此举是严格执行 VISA全球业务运作规章。VISA解释称，当VISA卡进行跨境交易时，如交易被其他支付品牌的网络处理，一旦发生交易纠纷或数据泄露，将会给持卡人带来极大的风险。<br />
<br />
VISA这一动作对中国境内的持卡人而言，意味着从今年8月起去境外旅游购物无法像往常一样“用中国银联的通道支付”，而必须用VISA清算。这样直接导致的后果是，持卡人可能多付1%至2%的货币转换费。同时消费之后如果享受免息还款期，持卡人将承担汇率变动风险，假使美元对人民币升值，持卡人在购汇还款的时候还要承担汇兑损失。不过如果在免息期间，人民币对美元升值那么持卡人也可以从中获得些实惠。银行人士提醒持卡人，8月之后如出国旅要带上一张有 VISA或MASTERCARD标志的银行卡。<br />
