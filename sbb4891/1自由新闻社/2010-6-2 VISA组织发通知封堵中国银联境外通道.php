<br />
银联成立过后，将各家银行的卡全部整合进来，进行卡号备案，银联能够识别这些visa卡，于是银联独自在中国处理这些visa卡的交易。这个时期前后，有银行开始发双币卡，双币卡的特点就是本身是一张visa卡，银行同时将这些卡信息备案银联，因为银联和visa是完全隔离的，于是一张卡片在两个网络上使用，互相不影响。银联当时和visa的关系非常好，因为银联几乎都是这些国际信用卡巨头培养出来的。<br />
