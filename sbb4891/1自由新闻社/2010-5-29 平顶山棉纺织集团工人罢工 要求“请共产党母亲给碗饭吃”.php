													<h2>平棉罢工---胡子兵法第三十七计</h2>
						
						
																											<div id="postmessage_650566" class="t_msgfont">今天是5月27号，平顶山下起了连绵的小雨，罢工工人在雨中艰难的支撑着，很多人哭着说家里已快断粮，各大媒体继续保持着沉默。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;我在10点钟赶到罢工现场，有几个工人拉着我讲述了5月25号黑社会打人的经过和真相。5月25号凌晨，坚守罢工的工人发现有不明身份的人在现场探头探脑，然后一闪不见了，不一会有大批的警察和警车，还有没有标示的车来到了罢工现场。工人们紧张的注视他们不知所措，这时，从一辆车上下来十几个不明身份的人，上来就分别抓住几个罢工女工的头发按在了地上，女工拼命挣扎，其他人都则把工人的帐篷掀翻，把标语撕掉，愤怒的职工一起上前将它们团团围住，这几个人见人就打，<br />
<br />
&nbsp;&nbsp;工人们都恪守着法律坚持打不还手，一直站在那，里三层外三层不让他们离开 。全体罢工的女工们彻底的对这些王八蛋死心了，只希望警察还能有些良心就全体跪在了警察面前 祈求他们凭着良心制止这些人的野蛮殴打，良心还没有泯灭的警察迫于这种场面，才将几个不明身份的人扣押，送伤者去医院治疗，其中一个伤者至今还没出院，可打人凶手却早被警察放跑了。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;工人们讲，当时幸亏工人们没有还手，其实，这是某些人设计的毒计。他们以为派几个黑社会成员动手殴打女工，那些男工人就会还手，这样，他们就可以大肆的抓捕工人，然后以暴乱的名义驱散罢工工人，可是，工人们没有上他们的当。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;听完讲述我的心一阵阵抽经，这是什么？我不敢去想，还是谢谢那些良知尚存的警察吧，要是他们不可怜这些工人，我想早就是“不明真相的群众，被几个别有用心的人鼓动发生的动乱了”，这时我才知道来了那么多车是干什么的。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;不能再说了，我自已也越来越感到不安全了。</div>

							
							
							
							
