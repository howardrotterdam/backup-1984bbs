“最美童声”杨沛宜<br />
<br />
有望接班王菲 杨沛宜大翻身 <br />
<br />
在重拍了《歌唱祖国》MTV后，杨沛宜参加了一场慈善音乐会，后又压轴香港举行的“新中国成立60周年庆典晚会”。与一年前那个“丑小鸭”相比，杨沛宜已经越长越可爱，香港许多娱乐圈人士预测：如果好好培养，杨沛宜将会有璀璨的未来。果不其然，随着《喜羊羊》热映，杨沛宜演唱的《别看我是一只羊》、《左手右手》立马走红，尤其是《左手右手》，该曲已经成了时下最热门的手机彩铃之一。而杨沛宜宛如天籁的歌声也赢得了很多观众的认可，甚至有人表示，好好发展，杨沛宜有成为天后王菲接班人的潜力。<br />
