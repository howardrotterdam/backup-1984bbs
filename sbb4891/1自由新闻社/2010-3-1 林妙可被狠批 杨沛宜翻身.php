林妙可参加春晚彩排(资料图)<br />
央视虎年春晚的话题不会就此打住，就在观众对刘谦和赵本山的议论渐渐平息时，林妙可在春晚上“抢麦”的举动又引发网友热议。其实“抢麦”本身事小，但有好事网友却翻出了“假唱”风波，天价代言等旧事，还搬来了濮存昕、陈小艺等前辈责其“说话太假”的忠告。回首林妙可的星途，还真是争议不断，再观其“冤家”杨沛宜，作为当年的B角，杨沛宜在签约公司后稳步上升，大有赶超A角的势头。 <br />
<br />
众网友批：小小年纪 心机太重 <br />
<br />
春晚中儿童歌曲联唱《我们和祖国一起成长》中，最后出场的豆豆、李木子、林妙可三人先是小合唱，继而在副歌的和声部分，要将手中的话筒交到蒋大为、李谷一、胡松华三位老艺术家手中。但林妙可在表演过程中，没有像李木子和豆豆一样将话筒交给老艺术家，而一直是自己拿着……由于春晚的极高关注度，林妙可的这个动作遭到了一些网友的“讨伐”，其主要内容就是批评林妙可对胡松华老师的不尊重，小小年纪心机太重，爱出风头。 <br />
<br />
对于“为什么要交接话筒”，林妙可的家人在其官网回复：下一个环节让几个孩子给李谷一等系红领巾，话筒在谁的手中，导演组并没有明确规定，只要能快速地完成系红领巾动作，可以交过去，也可以自己拿着。既然自己能做好的事情，为什么还要让老艺术家们操劳呢？因此林妙可家人认为其“麦霸”举动，不是不尊重胡松华老师，而是在为胡老师分担。 <br />
<br />
陈小艺叹：说话太假 声音太嗲 <br />
<br />
其实这可能就是小女孩的一个无心之举，但好事的网友却不这么认为，除了“麦霸门”，有人还算起了之前“假唱”风波，天价代言等旧账，指责林妙可小小年纪却圆滑世故。网友的评价可以置之不理，但无风不起浪，回首林妙可的星途，还真是争议不断，连老一辈艺术家濮存昕也对林妙可身上那种与年龄不符的“老练”感到费解，直叹“太多的拍摄，太多的老师教她，我怕她已经不是个孩子了。我教她说话就自自然然地说话，这个孩子应该停一停，把她已经会的那一套忘掉，真正像一个孩子一样去上学和成长，真的想干表演那是高中以后的事情。”此外，与林妙可有过影视合作的陈小艺也曾在采访中批评林妙可“说话太假，声音太嗲”。 <br />
<br />
与张学友同台涉足演艺之路 <br />
<br />
“在困难来临的时候，请你举起你的左手……”影片《喜羊羊2》热映，片中那首《左手右手》，感动了无数的孩子和爸爸妈妈，而演唱这首歌的孩子，正是奥运会开幕式《歌唱祖国》的“原声”、林妙可的B角――杨沛宜。 <br />
<br />
奥运会后，这两个互为AB角的小女孩便成了一对“冤家”，人们在谈论其中一人时，总是不自觉地将其与另一人做比较。从“双簧”事件曝光后，很长一段时间里，杨沛宜和家人都被失望情绪包围。虽为B角，但杨沛宜也一夜成名，不过，面临各种席卷而来的名誉邀约，小女孩的天生抵触，以及父母的坚决反对，让杨沛宜在近一年时间内默默无闻，直到去年10月2日，杨沛宜在香港特区政府隆重举办的“新中国成立60周年庆典晚会”上，与张学友领唱根据温家宝总理的诗作创作的歌曲《仰望星空》后，大量的消息才曝出，杨沛宜已经在娱乐圈高手的安排下加盟了金牌大风，正式涉足音乐的演艺之路…… <br />
