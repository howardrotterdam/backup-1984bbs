																											<div id="postmessage_743073" class="t_msgfont">&lt;《草见人命》<br />
作者：李红豪<br />
我想，在中国，能像尖毛草如此根深蒂固的，也只有此物。 　　<br />
《左传》中说“天有十日，人有十等，下所以共上，上所以共神也”，此话正合司马光“贵以临贱，贱以承贵”之意，其实质便是专制主义。千年历史中无论朝代怎样兴衰更替，专制主义都在封建皇权的呵护下薪火相传生生不息。后来再加上苏联模式的入侵，专制主义更是如日中天，直至今天这样名亡实存。 　　<br />
鲁迅先生在《青年必读书》中明确表态，希望青少年多读外国书，少读或不读中国古籍。他怕的不是传统文化，而是文化传统，是儒家的那张纲纪伦常的大网。孔夫子的“民可使由之，不可使知之”让鲁迅感慨，“不错，孔夫子曾经计划过出色的治国方案，但那都是为了治民众者，即权势者想的方案，为民众本身的，却一点也没有。”如今一个青年想读书，西方人会建议从卢梭的《论人类不平等的起源和基础》读起，而中国人则会语重心长的力荐道：“孩子，我们中华民族的文化博大精深，我们中华民族的历史源远流长，去读读四书五经吧，那里有你为人处世的准则。”这样读过几千年之后，中国人都变乖了。“博大精深”配合着“源远流长”，组团忽悠了无数中国人，看样子还要接着忽悠。 　　<br />
在我们的教育上，专制主义更是被演绎得登峰造极。老师中心老师主宰，老师说的话无论对错你都不能反驳，否则你便犯了顶撞之罪。如今各学校都修炼出了将一张圣旨变成一张剩纸的必杀技，这一点从放假、收费等事便可看出，而老师们耳濡目染之后终于将前辈们拍在沙滩上了。所以现在何为校规？当然是校长的规定啊，那班规自然是班主任的规定了。这样一来老师们独裁起来便都名正言顺，于是所谓的副课们均纷纷以闪电般的速度败下阵来，古德里安将军见状定会慨叹后生可畏。希望体育课不要也像萨达姆一样，悲情地死在霸权之下。 　　<br />
专制主义弥漫在各个角落，大到政权，小到试卷。比如语文卷子上的古诗文阅读，你知道千百年前这些人写诗词的时候内心是什么感情？教育者们首先不允许古人有七情六欲，然后说，我知道，这句话表达了李白的放荡不羁，这句话表达了杜甫的忧国忧民，答别的不给分。现代文阅读也一样，毫无意义的题目美其名曰“提高分析能力”。所以，现在的语文课其实是数学课，现在的母语课其实是英语课。这不是我不做前面题的原因，而是前面题根本不屑做的原因。 　　<br />
以上是行动的专制，其实任何行动的专制都是思想的专制。罗素很早就发现，“从上学时间看，中国儿童应该最有思想。”后来他失望了，他意识道，“一部分儿童具有爱思考的习惯，教育的目的就在于铲除他们这种习惯。”前段时间在内蒙古发生了小学生背领导名字一事，我想，原来等级专制观念也要从娃娃抓起。但有些人就是要思考，反帝反封建反专制，为之奈何？高考作文规定告诉你，讽刺我们就是零分。可见这讳疾忌医之态颇有日本历史界的风范。这霸王条款的规定加上黑格尔的理论又为教育专制主义提供了坚实的理论支撑，因为黑格尔告诉我们，凡是存在的都是合理的。 　　<br />
说到黑格尔，我又想起了我们的哲学教育。我们不重视哲学教育本身已是大错特错，而把哲学这样教育更是错上加错。其实哲学这个东西根本没有对错，只有喜欢与不喜欢。而我们的哲学教育一意崇尚马克思同时排挤其他学派，批唯心批宗教批有神论。其实，即使说我们崇尚辩证，也没有做到真正的辩证，因为很多知识的教育都是形而上的理论灌输加形而下的机械训练,犹太人自豪地称之为“生吞之功”。这里便涉及到功利主义的问题，我们的哲学太为无产阶级政治服务。老师们总喜欢警戒我们，学习不能太功利。听到这话我笑了，我记得布什好像也曾经说过，世界应该和平。 　　<br />
马基雅维利若是活在今天，我保证他睡着都能笑醒，他思想的火花在欧洲大陆已经熄灭，没想到在中国春风吹又生。<br />
