													<h2>高中生写敏感题材作文被评自毁前程 已出版小说</h2>
						
						
																											<div id="postmessage_743023" class="t_msgfont">高中生写敏感题材作文被评自毁前程 已出版小说<br />
<br />
南方周末记者 李宏宇 实习生 赵大伟 发自武汉<br />
<br />
<img src="https://yopic.us/images/lxllxl.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　李红豪现在还不满19岁，天蝎座，看过很多“杂书”，擅长用名人名言力撑观点。 李红豪/图<br />
<br />
<img src="https://yopic.us/images/2lol.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
作文《草见人命》批注版，原来的作文在规定的作文格里完成。与原稿作文有些出入。 李红豪/图<br />
<br />
　　李红豪告别华中师大一附中高二(27)班的教室，已经14个月了。<br />
<br />
　　华师一附中在湖北省声名赫赫，是数一数二的省级重点中学。学校网站的“知名校友”栏目里，有上百位博导、院士、将军、书记、委员、董事长、总经理，也有少数几位文化领域的知名人物如易中天、蒋方舟。<br />
<br />
　　2010届高中毕业生，也就是李红豪的一千六百多名同学，如今已经拿到了各自的高考成绩。有406人分数超过600，学校初步统计的理科重点率达到84%。<br />
<br />
　　李红豪既不在这84%之内，也不在这84%之外。他手里只有一份语文试卷答题卡的复印件。14个月前，高中二年级的期中考试，语文这一科，他交了份“另类白卷”――除了作文，答题卡上一片空白。题为《草见人命》的作文得到了25分(满分60)，以及力透纸背的四字评语：“自毁前程！”<br />
<br />
　　他的班主任说，不改正思想，就不要再进教室。于是他至今再没进过教室，尽管学校并没有正式宣布给他任何处分。他用半年时间写了一本22万字的小说，名叫《逃花园记》，自掏3万块出版。这个轨迹很像当年韩寒的模样，李红豪不这么觉得：“韩寒是主动不上学了，我是半主动……应该说是被和谐了。”<br />
<br />
　　《草见人命》原稿摘登<br />
<br />
　　罗素早就想，“从上学时间看，中国儿童应该最有思想”，后来他意识到他错了，“一些愚民的教育正是旨在铲除一些儿童爱思考的习惯。”前些时候曾有“小学生背领导名字”一事，我想，原来“官本位”的等级专制也要从娃娃抓起。<br />
<br />
　　……<br />
<br />
　　高考作文评分规则会告诉你“不得讽刺社会、教育、高考”，否则处以极刑。这做法颇有日本历史届的风范。这样一来，中国人便乖了。这霸王条款的明文规定加上黑格尔的理论为中国提供了强大的专制主义支持，因为黑格尔告诉我们，凡是存在的都是合理的。<br />
<br />
　　这篇作文可能会惹事<br />
<br />
　　高二语文期中考试是2009年4月23日。前一晚李红豪睡得不好，早上在学校食堂吃热干面，不知为什么又吃得胃里不舒服。“总是昏昏沉沉的，我已经打算不考了，准备跟老师讲一声。”李红豪回忆，“卷子发下来，看了一眼，也没有真正考语文修养的题。”<br />
<br />
　　但是看到作文题，他突然就像卢梭看到了第戎科学院征文的题目。卢梭在《忏悔录》里是这么写的：“一看到这个题目，我登时就看到了另一个宇宙，变成了另一个人。”<br />
<br />
　　考题要求根据一段文字材料作文，文字大意是：非洲有一种叫尖毛草的野草，别的草都在往上疯长时，它却一直往地下深处扎根，所以表面看起来长得很慢，但风雨一来，其他草都倒了，而尖毛草却岿然不动。<br />
<br />
　　按照“正常”的应试思路，题目的寓意很好分析：在学习上不好高骛远，要打下扎实的基础。但李红豪不知被点了哪股邪火，提炼出“根深蒂固”四个字，来形容教育中的专制主义。<br />
<br />
　　在被认为“自毁前程”的这篇作文里，判卷老师勾出了如下字句：<br />
<br />
　　“……我想，原来‘官本位’的等级专制也要从娃娃抓起。”<br />
<br />
　　“老师说的你不能反驳，不管他说的对不对，否则你便犯了‘顶撞’之罪。”<br />
<br />
　　……“要按正常写，还不是那些陈词滥调，我们的所谓作文写来写去就是那些东西。”直到南方周末记者问起，李红豪都还没有设想过如果按照“常理”他会怎样写这篇作文，“一篇作文可以套十个题目，改动几句话就套上去了。我初中就这么过来的，一篇作文用过七八次。如果你上过10年语文课，就会深深地有这种感觉，但是没办法，试卷上的分数在大家脑子里是根深蒂固的。”<br />
<br />
　　期中考试如此重要，他居然敢打算不考了，记者表示不解。李红豪很淡定：“对高中来说，只有高考才重要。”<br />
<br />
　　考完试隔了一个双休日，语文卷子发下来，唯独没有李红豪的。“那时候我在外出差，他给妈妈发了条短信：卷子没发下来，这篇作文可能会惹事。”李父告诉南方周末记者。<br />
<br />
　　周一早自习，班主任胡老师把李红豪叫出教室。“他说不改变思想就不要再进教室上课。他给的条件很苛刻：再也不能写这些东西，再不能给学校挑刺，不能讽刺学校、老师、政策……还要把爸妈请来，一起到年级主任办公室，一起教育你。”李红豪回忆说，胡老师还要求他转班甚至转学。<br />
<br />
　　他没想到“胡哥”这么大的火。平时他常常以“胡哥”称呼这位40岁的班主任，“我们是很好的球友，一起打球踢球，关系非常好；我们讲足球时，怎么讲都可以，但一说到教育问题，就针锋相对互不相让。”后来跟同学讨论此事，李红豪觉得这篇作文恐怕只是导火索，他不是第一次惹恼老师们了。<br />
<br />
　　不是我出头，是其他人的头缩回去了<br />
<br />
　　“我在班上属于激进派，对学校一些比较迂腐的规定，很多人敢怒不敢言，私下抱怨，但事到临头还是乖乖听话。最典型的事情就是买校服。”<br />
<br />
　　从2007年9月1日到2009年4月27日，李红豪在华师一附中一年半时间，已经买了4套校服，而他的大多数同学已经买了5套。最后这一套冬季校服，他坚决没有买。全年级一千六百多个学生，只有4个人没买这套冬季校服，都是李红豪班上的。“你可别以为是我带动的啊。”他笑着说，“我觉得这是教育商业化一个很不好的例子。我一个同学拿校服到江汉路服装批发市场一个做衣服的那儿，让他照着样子做，说是56块一套，可我们学校卖256元。这事全年级都知道，但还是要买。”“学校不让我上课，与校服的事肯定有关系。唉，说这种事，一天也说不完。”李红豪说。<br />
<br />
　　他把校服事件写进了小说。主人公陆谦在拒买校服这件事上，和班主任“飙哥”当着全班进行了一场唇枪舌剑的论战，飙哥最终无奈退场。论战中飙哥表示要“枪打出头鸟”，而陆谦柔声应对道：“不是我出头，而是其他人的头都缩回去了，我的头没有动。”<br />
<br />
　　这也是李红豪对自己的看法。<br />
<br />
　　《逃花园记》里，有陆谦跟老师们多次的舌战，有对宿舍管理、主课老师“霸占”副课时、考试牛人不知中国在哪一大洲等等各种现象的讽刺，有寝室好友分别的伤感，也有一段朦胧的校园初恋。<br />
<br />
　　李红豪离校约一个月后，开始动手写书。他翻看旧日记，发现“只要把主语一改就是很好的小说”。<br />
<br />
　　小说还写到主人公有次作文里抄了鲁迅的文章，结果被老师批得一无是处，这同样“来自生活”。“抄鲁迅的是一个同学，我抄的是林语堂《吾国与吾民》的几段话。”李红豪现在特别后悔没把那篇批改过的作文保存下来：“改得面目全非：用词不当，搭配不当，语句不通顺……还有个错别字，那是我自己抄错的――就这一个是我自己的问题，其他都是林语堂的问题。”<br />
<br />
　　李红豪对语文考试的不满从中考开始。他的中考成绩很不错，除了语文，其他科目都得到武汉所用“位置值”评分制度里的最高等级。但语文恰是他最擅长的科目。“中考一完，武汉报纸就会登出试题和标准答案。我看阅读题，发现我的答案比标准答案好多了，但跟它不一样。难道只有这一种答案？我又从阅读题想到作文，问题更大，同一篇文章，这个老师打60分那个老师打20分，这很正常……慢慢思考下去，中国教育最大的问题就是语文。”<br />
<br />
　　李红豪在网上认识了几个台湾网友，交流过语文试题。他认为台湾中学的语文考试才算得上“真正考语文修养”。在《逃花园记》的后记，他抄录了这样的题目为例：<br />
<br />
　　请名人代言是提高广告说服力的好方法。下列四则广告标题，如单就文字意义，寻找背景相契合的古代名人来代言，则最不恰当的组合是：<br />
<br />
　　A。请庄子代言“自然就是美”。<br />
<br />
　　B。请子路代言“心动不如马上行动”。<br />
<br />
　　C。请苏秦、张仪代言“做个不可思议的沟通高手”。<br />
<br />
　　D。请司马光、王安石代言“好东西要和好朋友分享”。不是反击学校，而是整个教育现状<br />
<br />
　　2009年11月书稿完成，李红豪找到长江文艺出版社。“到总服务台问了，编辑室在9楼，上去看见有两个编辑，我挑了年轻点的那个，估计比较好沟通。”<br />
<br />
　　小说的责任编辑杜东辉经常见到这样的“文学爱好者”，既有李红豪这样的学生，也有七八十岁的老人。“对一个高中学生，我觉得写得算不错了。引人发笑，可读性之外也还有一些思想。我觉得应该会有一定读者。”杜东辉也找了另外一些编辑商量，但多数人并不看好这部书稿，“因为市场上青春文学太多，很难冒出来。出版社也不可能花多大力气去宣传。”<br />
<br />
　　李红豪把这本小说当成对学校做法的一种反击，“也不是反击学校，而是整个教育现状。”他希望能尽快出版，甚至愿意自己分担部分成本。杜东辉说他也劝过这个孩子，“工薪阶层，这可不是小数目，未必都能收回来。我们也没少见作者自己投入出书，结果石沉大海。他妈妈也说过担心，她是希望儿子满足了这个心愿，回去继续读书，免得再去折腾。”他只好尽量为他们介绍了价格便宜的印刷厂。<br />
<br />
　　《逃花园记》首印5000册，零售价22元，总码洋11万。杜东辉告诉南方周末记者，如果一本不剩全部卖掉，李红豪的妈妈还可能稍有盈余。据他了解，这本书在武汉卖得还不错，尤其是华师一附中旁边有家书店，“第一次进了几十本，后来又进了百把本。其他地方像北京、上海，反响不怎么样。”<br />
<br />
　　在小说里，李红豪把中学生的作文比喻成大英博物馆，“里面有太多不属于自己的东西”。现在他出了书，但也并非就能把自己的东西尽数亮出来。最终的成书与原稿相比，还是被“和谐”掉了几千字，他全文附在后记中的那篇惹事作文《草见人命》，编辑觉得有点敏感，还是拿掉了。<br />
<br />
　　父亲对儿子出书还是支持和赞赏的：“要不说现在的小孩思路相当开阔，知道自己去解决一些问题。”他看了小说，认为“语言幽默、流畅，很真实”。他也关心儿子书里描写的那段初恋，究竟是否真有其事。李红豪说，恋爱情节是虚构的，“从来没碰到过蛮喜欢的。假如有的话，我想都不会想就会回学校了”。<br />
<br />
　　你知道我多想给清华大学校长写信吗<br />
<br />
　　李红豪从小很自由。上幼儿园时社会上已兴起“培优”风气，但父母从不强迫他上任何兴趣班。上学之后，他们也很少干预儿子。儿子离校在家一年多，没有主动表示想回去上学，父母也并不强迫催促。<br />
<br />
　　“我们过去的中学也是这样。”父亲说，“只不过我在这个年龄，就是赶快上学，高中也好大学也好出来之后赶快养家，没别的什么想法。他们现在接触的事情又多，世界上发生的，几分钟就可以知道，看书也多。”<br />
<br />
　　但李红豪和学校之间的纠葛，到底还没有一个解决。校方对他的事情三缄其口，南方周末记者多次拨打班主任胡老师电话，或者迅速挂断，或者无人接听。《武汉晨报》记者史强从半年前就多次联系校方采访，但颇为艰难。<br />
<br />
　　“我去了六七次学校，一开始没联系校长、高层，老师的顾虑比较多。反复找老师、各级领导核实细节，花了大量时间。”史强说。今年3月，他跟学校协商一致，陪着李红豪及其父母，与校方共同商量解决的办法，“开始气氛还好，谈到还原事件本身，李红豪情绪上来，局势失控。李红豪觉得委屈，激动，老师也觉得自己没问题。”最后还是僵局。<br />
<br />
　　学校也曾表示，不反对李红豪回校上学，但李红豪的家长觉得，得有个说法才能回去，否则在学校里抬不起头。“我觉得学校太武断。如果他违法，公安局会找他；如果他违反校规，那你就拿个白纸黑字的东西出来，违反了什么，怎样处罚；现在是完全没有说法。”李父宽厚温和，即使说到这个话题也并不显得激动，“写作文本身就要鼓励学生有独立的思考独到的见解，这才是个好学校，才是育人的地方。实际上我后来看了作文，我觉得一点问题没有。现在的情况是耽误了他的青春，做家长的非常惋惜，我认为他是一个人才。”<br />
<br />
　　校方起初一直不愿把那份作文试卷提供给媒体，史强第五次去学校，才终于拿到一份复印件。这倒是间接地替李红豪撑了腰。“假如这篇作文，问题真像他们说的那么严重，应该巴不得马上拿出来给全世界的人看――你们看这个孩子问题多大。”李红豪说。<br />
<br />
　　今年4月，李红豪虽已离校一年，同学还是把他叫回学校一起拍毕业照。现在除了出门参加同学们奔赴大学之前的各种聚会，在家看书、看世界杯，李红豪并没有固定的日常生活作息表，对于未来，也还没有明确的方向。<br />
<br />
　　如果他想再写这样的小说，很明显的难题是没有了生活素材。所以他也有点想回去“把高中生活完整地体验完”。也有老师建议他出国，并且可以提供帮助。“我现在最想的就是直接到大学。韩愈不也是科举考了几次，考公务员又一直考不上，索性直接给当朝宰相写信……所以才有《杂说》，写伯乐和千里马。”李红豪非常擅长从历史典故中为自己找到榜样。“你知道我有多想给清华大学校长写信吗？我又觉得不可能。”<br />
<br />
　　父亲也希望他能正常地上大学：“大学的资源毕竟不一样，有好的老师，图书馆那些书，也不是你自己想买就能买到的。你要写书，读大学也不耽误写书。”<br />
<br />
　　但有心接收李红豪的大学，恐怕需要有足够的气度与自信，因为他可能像李敖那样不正经上课，也可能再度批评大学的体制。<br />
<br />
　　李红豪还有个朦胧的想法，也许会写历史题材的小说。在时代上，他尤其钟情魏晋，最崇拜的人物是“竹林七贤”里的嵇康。“你有时间也该看看。”他郑重地向父亲推荐。“我没那个时间，”父亲说，“我还得挣钱养你。”<br />
