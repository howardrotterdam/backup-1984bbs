&nbsp; &nbsp;&nbsp;&nbsp;我院医务人员工作量高度饱和，每天上万人的门诊量，一百多台手术，临床之外，还兼有教学和科研任务，为此，大家陀螺般地旋转着，夜以继日地承受着工作的重压；即便如此，我们仍然满足不了全省各地乃至全国各地患者的需求；大家没有时间、没有精力去探究捕风捉影之事，也觉得没有必要在网络上徒费口舌之争，一一澄清事实，只能以“清者自清，浊者自浊”的心态将这些传言置之身外，希望这些谣传自生自灭。<br />
&nbsp; &nbsp;&nbsp; &nbsp;然而，事态的发展并不像我们想象得那么简单，也许是抓住人们对艾滋病的敏感反应吧，少数别有用心的人竭尽造谣惑众之能事，不仅歪曲事实，还杜撰情节，虚构人物，并借此大肆攻击医院和医务工作者，让许多不明真相的网友信以为真，并产生慌恐之情，以至于省人医的员工不断接到电话和短信的询问。这些已影响到我们的工作，损害了我们的声誉，伤害了我们的情感，让我们不堪其扰，也甚为不平。<br />
&nbsp; &nbsp;&nbsp;&nbsp;撇开我们自身不说，从另一个层面来看，艾滋病的存在和蔓延已是当今社会不争的事实，然而染患艾滋病毒并不是以行业和人群来界定的，任何行业的任何人都有可能被感染,即使有医务人员染患艾滋病，那也是可能和正常之事；医院是承担风险之地，医生的职业就是高风险的职业，外科手术的创伤、病毒的感染、放射性元素的侵害都可能对医务人员身体造成损伤，白求恩大夫就是死于手术中的病菌感染的。<br />
