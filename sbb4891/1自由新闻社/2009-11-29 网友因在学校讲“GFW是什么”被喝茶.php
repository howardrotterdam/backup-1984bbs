昨天晚上，网友广州职业技术学院的老师江南一笑邀请我和唐荆陵作为公共讲座的嘉宾与学生进行交流。<br />
<br />
江南一笑是体育部的老师，但他平时在公共课上经常给学生传授普世价值观，讲课过程时常结合一些反映公权侵害私权的案例，并且让学生观看外媒拍摄的被封杀视频。很受学生的欢迎，但也有学生告密，几周前，他的领导含蓄的暗示他如果继续从事这方面的宣传，不仅面临停课并且有去职危险。<br />
<br />
前天下午就收到唐荆陵的短信：江南邀请你明天听他讲课。我以为只是邀请听课如此而已。<br />
<br />
话说到了中午的时候，老唐再次跟我确认了一下约的时候。江南约我们5点在校门口，他来接我们。4点左右我到了岗顶发现那边一直在修路，乱得一团糟，根本打不到车（话说一直不明白为什么中国城市的街道年年修年年改）。5点45的时候一看，都快六点了，实在不好意思让他等，就赶紧打了电话给他，让他不要等我。好不容易在6点30打到车，一路塞塞停停，7点前终于到课室等他。<br />
<br />
7点，学生陆续到齐，江南先是痛批了一通**主义论之后，开始给学生放电影，这两个故事我都听过，第一个是央视制作的真实的新闻纪录片：一个是男人任自己的女人在屋内被人轮奸却不敢报警的事。第二个故事发生在某市44路公交车上，全车乘客遇劫匪洗劫无人反抗，女司机遭遇劫匪轮奸，一男子挺身而出却遭遇毒打。事后，女司机赶男子下车，将其行李扔出窗外。男子只好拦过路车继续赶路，途中遇一交通事故案发现场，原是他先前所乘女司机开的44路车，全车坠入山崖，无人幸免，他顿然明白一切。<br />
<br />
放完上面两个故事之后，他继续给学生放了太石村纪实影片，这时突然从教室的后门走进来2位领导，他马上叫前排学生将灯光打开，屏幕上的幻灯片在灯光的映照下，没了影像。他拿起话筒，一本正经的开始批判硬气功。两位领导听硬气功没听出啥名堂，几分钟后马上离开了教室。这时，江南又让学生马上关灯，继续播放纪实影片，几秒中之后，屏幕太出现了唐荆陵的特写镜头，镜头锁定，他请老唐上台，并向大家现场介绍老唐。老唐意识到领导并没有离去，只在讲台上匆匆简单介绍了一下太石村维权失败的经过就坐下，江南就笑了一下说：我原来以为你至少讲半个小时，没想到你讲了几分钟就结束了。<br />
<br />
有了他这句话的暗示，我明白老唐和我要讲的话题是他这次上课的重点。接下来，他就让我上台讲推特的好处和使用方法。因为我想到很多老唐该讲的话没有来得及讲，我就借题发挥，先从什么是GFW开始，然后讲到为什么要用推特。我问学生：大家刚才看到的视频在其它地方有看到过吗？齐声答：没有！那么，为什么徐老师会有这些视频呢？大家知不知道我们的电脑很多网站不能访问？有一个学生回答：因为有GFW。<br />
<br />
从GFW开始，我就拿国家来作比喻！说国家是一小区，我们是业主，政府是物业管理，我们交钱给政府，叫政府管好小区，并没有说让物业堵住大门不让我们出去，也没有说让物业封住网络不让我们看外面的世界，但这个物业管理强行不让我们出去，还不让我们随便上网，我们为了随便上网就要学上推特！然后开始讲解推特的特点，其信息传播的高效性，准确性，及去中心化，这样话语权掌握在每个人手中，每个人都可以发布新闻……(但你发布的新闻跟你本身的信用有关，如果你发布的是假新闻，那么关注你的人会锐减，你的新闻就越难发出，如果你发布的新闻很有价值，你的关注度会飙升。总之，推特实行的价值观是鼓励向善，摒弃作恶！)括号里的话没来得及讲，这时一个学生称肚子疼离场，江南过去询问她，并扶她离场，老唐也随即起身一起离场。江南扶她离场后，进来教室跟我说不能再讲课了，我当时脑袋还是懵的――这段时间甲流一直没好，大脑始终处于停滞关状态，但他跟我说先休息一会时，我马上明白他的意思，立即找个靠近的座位坐下。他马上宣布课间休息10分钟。我抓紧时间给老唐发了个信息，问他在哪，他说在楼下等我们。<br />
<br />
他刚宣布完信息，就有身穿灰色套装领导模样的人，进来走到我坐的桌子前，学生们就围过来。这位领导蛮横的教训我：“你刚才在台上说了什么，你知不知道这三尺讲台不是随便什么人都可以上去的。你有教师资格证吗？你是什么人，凭什么走上我们的讲台？”<br />
<br />
我：“我只是跟同学分享一些使用网络的技巧，谈不上讲课。”（其实我可以理直气壮的反驳是江南老师邀请来的嘉宾，受邀的嘉宾有权利，有资格与听众分享心得，但我不想将他牵扯进来，也不想给这位官员我们有“串通一气”的口实，所以我只这么回答他）。<br />
<br />
 他继续反复用刚才的两句话教训我，我沉默。几分钟后，我起身走出教授，走向电梯，这是他和另一位略胖，年纪4－50岁的穿着白衬衣黑马甲中年人一起跟了过来，应该也是个领导。灰衣男人还是气势汹汹，咄咄逼人的要我交代清楚：“什么人叫你过来，你在课上跟学生讲了什么？……”<br />
<br />
我回他：“没人叫我过来，我只是知道这里有一个讲座，并且，我也有一些网络应用方面的技巧跟同学们分享，别的无可奉告！”这时电梯门开了，里面有人，他示意里面的人关门。于是我转身走向楼梯，结果，两人就伸手作拦我状，但因为那里进出学生较多，他们也有怕顾忌，也没真的动手，但一边还继续要求我跟他们去保卫科。我也毫不客气：“我没有犯法，没做什么治安违法事件，我不接受你的无理要求。叫我跟你走不可能，有种就叫警察来拷我走。”<br />
<br />
他马上就掏出手机，跟110打了电话：“喂，XXX吗？（他不是称呼110报警中心，应该是直接找的派出所的什么领导）这边有事，10万火急你马上派个人来我这里。”<br />
<br />
听到他跟对方这么说，我也怕110要是跑到教学楼上来就麻烦了，那给江南的影响就太大了，我立刻快步走下楼梯，俩人也紧跟着我下楼。一出教学楼大门，就看到老唐站在门前等我，我本能的走上前想跟他说话，突然意识到跟他说话可能又把他牵进来了。<br />
<br />
这时老唐听到他们的训话，就过来跟我们搭腔：“这位先生，没有规定只有教师才可以讲课吧，再说了，这位小姐没有跟你们订合同，不是你们的正式教员，也不需要什么教师资格吧，作为受邀嘉宾，任何人都有资格跟听众分享一些东西。”（我暗想老唐果然是律师，一出手就是厉害）。<br />
<br />
被老唐一驳，马上一愣，但灰衣服那人继续强词夺理：“谁说嘉宾有资格上讲台，没有经过我们领导允许，你有什么资格站在讲台上？”<br />
<br />
老唐也毫不客气：“嘉宾当然有资格，只要是老师邀请的嘉宾当然有资格。”<br />
<br />
我不愿意他提到我是江南邀请过来的，我马上就打断他：“没有什么人通知我来这里，我只是看到通知知道这里晚上有讲座，就过来了，同时我也觉得我有一些心得和体会想跟同学们交流，谈不上讲课，我不是讲课，分享也只是一些电脑应用技术上的问题。”<br />
……<br />
<br />
我一边说一边继续想向前走，他们不停的纠缠，阻拦，终于是没能走到学校的大门口，警察就到了，他过来盘问了我的身份，我拒绝给他：“请问警察可以任意盘查没有任何违法行为的公民身份吗？”<br />
<br />
“警察可以在任何时候盘查任何一位公民的身份。”他振振有辞的回答。<br />
<br />
老唐这时就问他：“请问你的警号，姓名。”我也凑过脸去看清他的工作牌上写着“姓名：冯波”。<br />
<br />
他没想到一个“路人”竟然敢这么大胆的问他这个，有点恼羞成怒，叫老唐也拿出身份证。老唐不想惹事，还是把身份证交给了他，登记好。丫叫来另一辆警车把我和老唐以及另一句警察塞到后座。我们坐下后，他们又叫学校的一名看样子像保卫科的人进来，副驾驶空着，不让坐，非让四个人挤在后座。妈的，太可恨了！让我一个女生跟三个男人混挤在后座上！<br />
<br />
到了派出所，直接让我们进了铁门后面的留置室，话说我来这种地方不是一次两次，但是还是第一次直接关到铁门后。进了留置室，里面坐着2名保安和一句便衣，那便衣长得瘦瘦小小一副发育不良的样子，认真看还有一点女相，脸小得可怜，俗称的巴掌脸是也。公仆们真会节约，居然请保安来值夜班。进屋坐下后，老唐跟朋友们发短信，通报情况，我也用手机发一条短信到推特上更新：被警察去，因槲医V州民族W院W生上推特，靠，此公W叫T波。顺便也把这条短信给2可器发了一遍，没想到这小子回我：靠，恭喜，怎么被发现的？<br />
<br />
便衣勒令老唐不准再玩手机，否则没收。我气不过，说道：“公民有通信自由，再说我们现在又不是罪犯，为什么要限制人身自由？”<br />
<br />
他瞪了我一眼，凶道：“进来这里的人都不准在这里打电话发短信。” 我不想跟他争吵，大家都沉默。<br />
<br />
老唐把手机收起来，从包里掏出一本书看，我觉得疲倦，眯着眼休息。很快一名长官模样的人进来叫老唐到隔壁问话。几分钟后，换我去问。内容大体是我哪里人，什么工作的，来学校干什么。我的回答：广州人，没工作，来学校跟学生分享上网技巧。<br />
<br />
原以为问完之后，很快就可以作笔录，放我们出来，没想到继续把我们晾在留置室。其间报案的女人也被抓进来，原来是被劫的，我表示关心的问了几句，那巴掌脸便衣又喝斥我不准讲话。我忍住怒气，不再吭声。11点左右，听到外面大厅一个警察接一个投诉电话，气急败话的对着电话嚷嚷：“你可以投诉我，也可以……”，后来知道原来是老唐的太太打电话跟这边交涉放人，她言辞犀利，警察有点招架不住了，留置室里的气氛一下子也紧张起来，那个便衣似乎感觉到压力。老唐太太那边电话刚挂，马上这头又打给老唐，老唐一接电话。那便衣立马过去把老唐的电话抢走。这下彻底激怒我了：你凭什么不许人家接电话，现在什么时间了，我们即不是犯人也没违法，你凭什么把我们留在这里到现在还不肯放人？连跟家里说一下都不可以，简直岂有此理？<br />
<br />
“你们是被调查的对像，我们有权留置你们24小时。”<br />
<br />
“那哪条法律规定我们不能给家里打个电话通报一下，再说了，别说我们没有犯法，就算有违法行为未经审判也不是罪犯，你有什么理由这样限制我们的人身自由。”<br />
<br />
“在这里就是不许打电话。”便衣显然知道这个事情理论下去，会吃亏，就一句简短回答。<br />
<br />
“我没看到这里哪里有贴出什么法律规定，你说的规定我也没看见。”<br />
<br />
“在这里我说的话就是法律！”<br />
<br />
“什么，你说的话就是法律？你再确定一下这句话，是这样意思吗？如果是的话，恭喜，你也会出名的。前几天广州有个官员说拉不拉屎要不要让群众知道，结果没几天，他就出名了，官职也丢了。”我说这句话的时候，老唐也忍不住笑了起来。<br />
<br />
那人一看情形不妙，没敢回应，但还是不客气：“反正在这里不能打电话也不能讲话。”<br />
<br />
我紧追不放：“是不是在这里你说的话就是法律？？？”<br />
<br />
丫吱吱唔唔，气焰消了半截。这时听到外面又是一阵吵吵嚷嚷，老唐说：“老袁和刘士辉到了。”<br />
<br />
随后，我和老唐再次被叫走问讯，估计他们俩不到的话，还得晾我们几个小时。老唐在问讯室，我被叫到大厅作笔录。袁新亭看我出来就过来问我：“怎么样，没事吧？”<br />
<br />
我一回头：“晕，你们干嘛来,跟你们有啥关系啊？”我很不客气，这话是说给警察听的，是怕连累他们，到时他们也卷进来一起笔录，那就不知道要折腾到啥时候了，原本老唐是可以脱身的，就是因为跟我搭腔给卷进来了。老袁没想到我这么不客气的凶他，有点委屈的回答我：“（我们来了，你们）不要那么孤独嘛！”，呵呵，当时心里蛮感动的，但不能在警察面前多说什么，只叫他赶紧到一边坐着！<br />
<br />
笔录做了半天，内容跟第一次问讯一样。不学无术的警察打字贼慢，一句话打了半天。我闲得无聊，东张西望，吃一口老袁买的八宝粥，发半天愣。正愣着呢，肩膀被人一拍，一回头原来是北风到了。180肥硕的块头，加上满脸的大胡子，气场十足。他问了一句我进来多久了，就对着警察说：我告诉你们，如果你们还不放人的话，会源源不断有人到这里来参观拜访你们的。你们派出所分分钟会出名！<br />
<br />
警察的工作明显利索了，十分钟之内搞定，放我们出门。<br />
<br />
