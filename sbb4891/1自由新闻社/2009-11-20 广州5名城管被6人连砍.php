													<h2>广州5名城管被6人连砍</h2>
						
						
																											<div id="postmessage_334448" class="t_msgfont">广州5名城管被6人连砍 作案团伙有预谋守候(图) <br />
来源：大洋网-广州日报 2009年11月20日　　时间 昨日8时45分<br />
<br />
　　地点 天河北穗园小区天阳街<br />
<br />
　　昨日8时45分，天河北穗园小区天阳街发生一起恶性事件。天河石牌街城管中队5名队员的执法车被6名男子截停。1名男子将刀伸入车内乱砍，造成两名队员重伤。<br />
<br />
　　事件经过<br />
<br />
　　●6名凶徒截停执法车。<br />
<br />
　　●城管摇下车窗了解情况。<br />
<br />
　　●1名凶徒突然把刀伸入车内乱砍。<br />
<br />
　　●5名凶徒堵住车门不让城管下车。<br />
<br />
　　受伤人员情况<br />
<br />
　　●两名城管重伤。<br />
<br />
　　●其余城管不同程度受伤。<br />
<br />
　　●一名凶徒轻伤被擒，其余逃离现场。<br />
<br />
　　本报讯 （记者卢迎新 通讯员田禾） 昨日上午8时45分，天河石牌街城管中队队员陈加高带辅助队员甘东全、王惠武、李明、吴宗宗等乘一辆执法车到天河北穗园小区天阳街执行巡查任务时，突然被6名操粤西口音的男子截停，待城管队员摇开车窗询问时，1名身穿橙色风衣的男子将刀（用报纸包着）伸进车里，猛地砍击城管队员，导致两名城管当场受重伤休克，其余城管队员受不同程度轻伤。<br />
<br />
　　据悉，身穿橙色风衣的男子将刀（用报纸包着）伸进车中猛砍城管队员时，另外有几人挡住车门，不让其余城管队员下车。猛烈的砍击，致使陈加高头部、肩部和手部被砍伤，甘东全头部、手腕被砍伤，两人均因流血过多休克；王惠武、李明、吴宗宗不同程度受伤。陈加高、甘东全被送到中山三院紧急抢救。<br />
<br />
　　经诊断，陈加高颅骨骨折、手部被多处砍伤；甘东全头部被砍出一条长约10厘米的伤口、手腕也被砍出长约8厘米的伤口。在城管队员防卫过程中，不法分子陈国清受轻伤后先被警察送往市六人民医院医治，后送往武警医院羁押病房，目前情况稳定。其余不法分子逃离现场。<br />
<br />
　　据中山三院方面介绍，目前陈加高生命体征总体稳定，甘东全等其他城管队员经过治疗处理后情况稳定。<br />
<br />
　　目前，天河区正组织有关部门全力做好该事件的侦查取证和善后处理工作。<br />
<br />
　　有前因：凶徒曾被执罚<br />
<br />
　　本报讯 （记者何道岚、陈翔 通讯员许健） 夜晚9时许，陈加高依然在ICU病房，他脑部挫伤，颅内积气，右手肌腱断裂；甘东全躺在急诊住院7室，神志清醒。主诊医生表示，两人头部的刀口长十几厘米，“看得出行凶者很残忍。”<br />
<br />
　　警方证实，被捕的行凶者是小贩，至于处罚方式还要经调查后方能确定。另外5名在逃男子，警方也正尽力追缉。<br />
<br />
　　甘东全告诉记者，行凶的男子是自己曾经执罚过的菜贩，对方也是明显认得他们才下手的，更像是有预谋的伏击。<br />
<br />
　　有预谋：凶徒守候多时<br />
<br />
　　本报讯 （记者全杰）“种种迹象表明，这次袭击肯定是有预谋的！”广州市城管局天河分局有关负责人直言对事件非常震惊，“我们调出监控录像，发现6名袭击男子都是早早守候在那里。”<br />
<br />
　　负责人说，6名男子年约20多岁，全部站在绿化带后面，手里拿着的东西用报纸包裹着。他们似乎对城管执法车行走路线和时间非常熟悉，作案时间也掐得很准。<br />
<br />
　　这位负责人承认，最近天河乱摆卖情况较为严重，城管除延长执法队员的巡查值守时间外，还增派一线执法队员，“现在连机关办公室的城管人员都要下到一线巡逻守点，一直到晚上11时。”<br />
<br />
　　是不是因为高压打击态势而导致事件的发生呢？这位负责人表示不好猜测。不过，有一线城管队员向记者表示，一些“走鬼”视城管队员为“眼中钉”，打击报复的事情时有发生。<br />
<br />
　　但该负责人强调，尽管加强了执法力度，但都要求执法队员采取人性化方式，对乱摆卖只能采取“教育、疏导、服务”的手法，“我们都要求队员千万不能硬来，遇到暴力抗法都要先离开，然后马上报警，让警方来处理，绝对不能产生直接冲突。”<br />
<br />
　　城管遇袭时有所闻<br />
<br />
　　2007年11月7日<br />
<br />
　　南沙城管大队会同南沙街街道共同清理创卫黑点，在依法拆除一违建窝棚时遭暴力抗法，负责摄像取证的城管队员程国文被砍伤。<br />
<br />
　　2008年8月20日<br />
<br />
　　天河城管大队天园街中队队员马进京巡查到广海花园路口时，突然被一名男子挥刀猛砍。马进京说：“凶徒好像是卖手袋钱包的。”<br />
<br />
　　2008年12月24日<br />
<br />
　　市城市管理综合执法局花都区分局4名城管对一无证烧烤摊进行检查时，被几名食客阻挠。一食客捅伤两名城管，其他三名食客用啤酒瓶砸城管。<br />
<br />
　　有此说法<br />
<br />
　　双方冲突达5分钟<br />
<br />
　　“操粤西雷州口音的年轻人在摆摊卖货品，城管上前收缴，双方起了冲突。”<br />
<br />
　　“持刀青年向离自己最近的一名城管头顶猛劈，顿时血流如注。”<br />
<br />
　　本报讯 （记者卢迎新 实习生周圆）记者昨日来到事发地点――天河北华标广场旁金太阳幼儿园门口至真功夫快餐店路段了解事发时的情况。<br />
<br />
　　管“走鬼”起冲突<br />
<br />
　　据士多林老板回忆，昨日上午近9时，他的店铺一名卖东西的工仔看见：6个看上去大约16岁左右、操粤西雷州口音的年轻人在摆摊卖货品，城管上前收缴，之后双方起了冲突。<br />
<br />
　　之后，听见嘈杂吵闹声的林老板赶紧出来看个究竟。只见几个操雷州口音的年轻人中，其中几个正欲离开，往天河北方向走去，剩下一名年轻人正在用一把约80厘米的西瓜刀拍城管车的车窗（此时多名城管在车上）。<br />
<br />
　　“走鬼”刀劈城管<br />
<br />
　　突然，持刀青年向离自己最近的一名城管的头顶猛劈，“当时那名城管队员血流如注，甚至见肉了，好像看暴力电影一样。”据林老板回忆，看到同伴和城管打起来，几个本来想要离开的年轻人又回来加入打斗队伍，双方发生激烈冲突，“这时候又看见另外一名穿制服的城管头部受伤，捂住头走回来。”<br />
<br />
　　“整个冲突过程大概5分钟，其中打斗过程约40秒。”林老板说。</div>

							
							
							
							
