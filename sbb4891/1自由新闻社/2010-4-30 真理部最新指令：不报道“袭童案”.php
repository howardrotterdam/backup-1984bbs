<br />
中国文明网　<a href="http://www.wenming.cn" target="_blank">www.wenming.cn</a>&nbsp; &nbsp;2010-04-30&nbsp; &nbsp; 来源: 中国新闻社 <br />
<br />
 2010年上海世博会将于5月1日在黄浦江畔盛大开幕，世界各国都期待能在这个舞台上展现自己的风采，对世博会的关注正日趋升温。<br />
<br />
&nbsp; &nbsp; 《非洲新闻时代》报道称，肯尼亚总统齐贝吉将出席5月1日上海世博会的开幕式并为非洲综合馆中的肯尼亚国家馆揭幕。<br />
<br />
&nbsp; &nbsp; 报道认为，上海世博会将为许多非洲国家提供展示自身的良好平台。通过世博会，非洲大陆可以吸引来自世界更多关注的目光。<br />
<br />
&nbsp; &nbsp; 安哥拉国家通讯社刊文称，2010年上海世博会安哥拉国家馆在展会期间将吸引超过600万的参观者。<br />
<br />
&nbsp; &nbsp; 安哥拉上海世博专员表示，安哥拉馆的面积超过1000平方米，这足以让更多的参观者前来感受安哥拉的独特魅力。他还透露，安哥拉国内的一些知名歌手也将前往上海，为世博会助兴。<br />
<br />
&nbsp; &nbsp; 挪威媒体也对上海世博会表达了关注，他们将报道的重点放在了挪威著名乐团“秘密花园”身上。这个在亚洲和美洲影响力颇大的音乐团体将为上海世博会挪威馆创作主题音乐。<br />
<br />
&nbsp; &nbsp; 报道还称，作为挪威参与上海世博会的一部分，“秘密花园”将和挪威馆一起将“挪威，自然赋予的力量”的布展理念传播给中国观众。<br />
<br />
&nbsp; &nbsp; 而靓丽的“挪威之夜”也将给予参观者特殊而绝妙的体验，让人们感受到挪威的自然风光、神秘的北极光和挪威人的日常生活。<br />
<br />
&nbsp; &nbsp; 印尼《雅加达邮报》在其文章中写到，印度尼西亚将在上海世博会中着重塑造自己良好的国家形象。<br />
<br />
&nbsp; &nbsp; 文章援引印尼贸易部长邦埃斯杜的话称，印尼将考虑在世博会中展现自己在经济发展和民主进步中取得的巨大成就。<br />
<br />
&nbsp; &nbsp; 上海世博园的印尼主题馆将充分表现一个文化和自然资源丰富，民众单纯友好的文明国家形象。<br />
<br />
&nbsp; &nbsp; 同是东南亚国家的菲律宾，也将有30多家公司出现在世博舞台上，他们将充分展现菲律宾在环保产品和绿色生活方式上取得的成就。<br />
<br />
&nbsp; &nbsp; 报道称，由菲律宾城市规划部门和工业技术部联合推出的世博菲律宾馆预计将吸引超过700万人次的参观者。<br />
<br />
&nbsp; &nbsp; 而与其他国家略有不同的是，在世博5个月的展期内，菲律宾将分别以不同的内容和主题展现国家在环保、信息技术和创意产业等诸多领域的非凡成就。<br />
<br />
&nbsp; &nbsp; 以色列世博项目大使毕雅丽称，以色列希望通过此次活动加强以中两国合作，并表示这是以色列首次建设自己的世博会场馆，政府为此拨了1200 万美元的资金。<br />
<br />
