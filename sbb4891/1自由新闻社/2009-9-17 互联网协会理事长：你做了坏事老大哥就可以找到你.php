																											<div id="postmessage_245105" class="t_msgfont">互联网协会理事长胡启恒:网络实名制势在必行<br />
2009-09-16<br />
中国互联网协会理事长胡启恒在“首届中国网民文化节庆典”上指出，实施网络实名制势在必行。但分析人士和法律专家认为，网络实名制有悖宪法精神。自由亚洲电台记者唐琪薇报道<br />
<br />
“首届中国网民文化节庆典”9月14号在北京举行，中国互联网协会理事长胡启恒在回答记者提问时表示：我认为实名制是势在必行，你可以在网络上登记自己我叫花猫、白孔雀，如果你没有违反社会公德，没有侵犯别人的利益，没有做违法的事情，你就叫花猫好了，但是你做了坏事就可以找到你。<br />
<br />
记者致电北京某网吧，网吧工作人员告诉记者，他们还没有听到有关实施网络实名制的消息，但是，来网吧上网的人是需要提供本人身份证的:“就是身份证，出示身份证验证码。”<br />
<br />
北京作家、资深媒体人凌沧洲告诉本台记者，去网吧输入身份证号码只能监控网民上网的时段，而很难了解网民去了那些论坛，发了哪些帖子，而网络实名制就不同了。不过凌沧洲认为，网络实名制真正要实施起来难度恐怕很大：“明知道这种口号、呼声已经呼吁好多年了，但有没有多大效果或者说能不能推行的下去，我觉得都是问题。因为网民发表言论，只要他发表的言论符合宪法,法律，那我觉得各方面都应该尊重啊，何况说他用网名或者是匿名也是为了更方便的来监督，避免打击报复。” <br />
<br />
事实上，杭州已经从5月1号起率先实行网络实名制，新的互联网管理条例要求网络论坛等必须实行实名制。条例推出以后，网民争议不断。杭州网民刘小姐告诉本台记者，自己本来还经常去“19楼空间”等一些本地热门论坛浏览、发帖，听说要网络实名制后就不再光顾了。刘小姐说，她在新浪有一个博客，登记的时候并没有用实名：“不用实名的，只要填一些网络需要的一些登记的资料――注册资料、手机号、或者你的邮箱要登记的。”<br />
<br />
刘小姐表示，如果注册的时候要求用身份证来实名登记，花猫、白孔雀这样的虚拟网名就失去了虚拟的意义：“那我肯定不愿意的。因为网络提供了一个空间嘛，就说可以说一些实名就不敢说的话我觉得。那么如果这样的话可能就会限制很多人比较自由的思想了。”<br />
<br />
中国政法大学法学博士滕彪律师告诉本台记者，几年前某些高校已经采用了网络实名制，学校的BBS论坛，只能用实名登记才能注册。滕彪说，网络实名制显然不符合宪法精神：“言论自由它包括匿名发表言论的自由。如果在网络上都一定要实名才能发言的话，那实际上就没有什么言论自由。”<br />
<br />
凌沧洲认为，在一个自由开放的社会，的确可以用实名制来让网民对自己的言论负责。但在中国目前的大环境下，匿名权还是能够对网民起到保护作用。凌沧洲说，有关部门要推行网络实名制的初衷也许是想维持社会稳定，但由网络实名制带来的副作用也不能忽视：“这言论自由的促进的副作用。如果你真的要促进这个社会的文明，促进社会的稳定和和谐的话，你首先去尊重、满足人民群众的意愿，把官员财产申报制这个阳光法案能够推动了，我觉得功德莫大。”<br />
<br />
据报道，韩国是目前全世界唯一实施网络实名制的国家，从今年6月起，韩国的35家主要网站已经按照韩国信息通信部的规定，陆续实施网络实名制。<br />
<br />
<br />
以上是自由亚洲电台记者唐琪薇的报道。</div>

							
							
							
							
