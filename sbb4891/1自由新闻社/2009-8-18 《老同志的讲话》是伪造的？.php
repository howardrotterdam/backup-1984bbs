																											<div id="postmessage_209131" class="t_msgfont">找了点资料，自己学习一下<br />
宪法序言是否具有法律效力，是目前关于宪法序言问题最有争议的问题。这一问题并非为我国所独有，西方国家也出现过这类的争论。在日本，宪法序言是否具有法律效力的争论是围绕宪法序言能否直接成为法院所运用的裁判规范而展开的。在法国，自1946年以来宪法序言的法律价值一直是个争论不休的“经典”问题。一直到1971年7月16日，宪法委员会通过对关于结社自由的决定确认1946年和1958年宪法序言的宪法价值而使该问题得到解决。另外，在德国学界也出现过关于宪法序言是否具有法律效力的争论。争论中，施米特认为宪法序言没有法律效力。 <br />
<br />
　　（1）无效力说的主要理由：第一，宪法序言中大多数原则性规定难以成为人们的行为准则，没有必要赋予其法律效力。第二，宪法序言主要是某种价值观的表述，其价值主要在于使宪法结构更具完整性，本身不具有法规范的属性。第三，宪法序言原则性的规定和事实性的叙述，其结构形式不符合也没有必要符合法律规范的结构要求，因而其法律效力也无从谈起。（2）有效力说的主要理由：第一，宪法序言作为宪法的构成部分之一，自然与宪法的其他部分一样具有法律效力。第二，宪法序言同宪法的其他部分一样，其修改也都遵守严格的程序。第三，宪法序言在正确解释宪法、使用宪法条文等方面起着越来越重要的作用，也即在现代宪法体制中宪法序言的职能作用日益体现出来，它具有构成宪法规则的规范性基础。（3）部分效力说的主要理由：上述两种认识都具有片面性，只针对宪法序言的部分内容，不能涵盖宪法序言的全部。因此，宪法序言仅具有部分法律效力，即它必须和宪法条文相结合，宪法序言才具有法律效力。（4）模糊效力说的主要理由：宪法序言的抽象性特点可能导致一种效力的未确定状态，即模糊效力的状态。模糊效力说肯定了“部分效力说”关于宪法序言的原则性内容须与其他条文相结合才能发挥效力的观点，主张以“模糊效力”一词来予以体现。 <br />
<br />
　　从新近出版、发表的文献资料看，我国现在较多的学者同意宪法序言有效力说，很少有人完全否定宪法序言具有法律效力了。也就是说，对宪法序言是否具有法律效力的争论已趋于平息。但还不能说，对宪法序言的效力问题已经没有争论了；而仅是争论的重点发生了转移：现在所争论的焦点是宪法序言法律效力如何表现，这个争论已将宪法序言具有效力接纳为一个确定的前提了。上述“部分效力说”和“模糊效力说”，本质上并不是关于宪法序言有无法律效力的争论，而是转向宪法序言法律效力如何表现问题的争论。”<br />
<br />
　　另外，参加过1982年《宪法》草拟工作的许崇德教授在全国人大常委会法制讲座上也指出：<br />
<br />
　　“有的同志问：宪法的序言和条文是否有同等的法律效力？这个问题在我国的宪法学界有争论。有三种意见：一种认为序言同条文同样具有最高法律效力；第二种意见认为序言是一篇文章，没有法律效力；第三种意见认为序言一部分具有法律效力，一部分没有法律效力。所谓一部分有法律效力指的是有规范的段落，比如对国家根本任务的规定，至于历史叙述部分就没有法律效力了。这三种观点，宪法学界尚无定论。<br />
<br />
　　我个人看法，序言应该跟条文一样具有法律效力，违背序言就是违宪。因为序言把具有重要精神实质性的东西作了规定，特别是规定了国家的总任务，‘一个中心、两个基本点’也在序言里，如果违背它，当然是违宪。”</div>

							
							
							
							
