<br />
日前一篇题为“执政党必须要建立基本的政治伦理”的文章在网上热传，据称是根据中共党内一名曾居最高领导层的元老同中央党校一名年轻教授的四次谈话整理而成，并经谈话人审定。海内外专家和网友推断谈话者是前中共高层元老万里(曾任国务院副总理、中央政治局委员、中央书记处书记、全国人大常委会委员长)，本台就此专访了《动向》杂志和《新世纪》网站主编张伟国先生。这是专访的下半部分。<br />
张伟国认为，整篇谈话的主题，就是主张通过重建政治伦理改变中国社会。（声音）“整个文章，八千多字，各个角度就围绕着政治伦理这样的眼来做文章的，他在里面也提到了共产党到现在为止没有登记注册，所以网上就有网友开玩笑，说中国现在最大的非法组织就是你中共啊，这样的一种现象实际上都在他的政治伦理中批判到，都是中共丧失政治伦理的一种表现了。”<br />
<br />
张伟国说，这篇谈话作者对中共内部运作机制非常清楚，他之所以采用互联网公开的方式是有原因的（声音）：“尤其对一位良知尚未泯灭的老人来讲，对于一位没有被中共一党专制机器同化的人来讲，他也需要对历史作一个交待。在今年Zhao Ziyang的“改革历程”出版以后，实际上在中共高层有很大的震撼，很多人都需要对历史，对自己的良知作出一个交待。Zhao Ziyang的“改革历程”象一个坐标一样，最后在离开这个世界的时候，你对你自己这一辈子所做的事情都要有一个交待，他需要对自己的良心，对人民，对历史有一个说法。”<br />
<br />
对于这篇讲话所引起的效应，张伟国说：（声音） “我们应该体谅到他在本意上对中共也不抱希望了，如果对共产党还抱有希望的话，他肯定是采用传统的，他们内部的渠道，他会写信，通过秘书，送到中南海，给各个政治局常委，或者政治局委员，他完全有这些渠道嘛，他现在完全舍弃那些方式没用，或者是那种方式他用了，同时他又用网络发表的形式，反映出很大的一点，他对原来的信息传递方式，共产党的执政方式他已经不抱信心，或者是没有充足的信心。通过网络的意识，一个是通过全党，一个是通过全民，通过舆论，寄希望于人民，寄希望于社会，我想这是他选择网络发表的一个很大的原因，这一点反过来也说明，这个作者本身对中共已经不抱希望了。”<br />
<br />
张伟国最后说（声音）：“共产党现在已经是无药可救的程度了，要凭这样的一个讲话来影响它，改变它的决策是不可能的，中南海无药可救，中共无药可救，是一个无法挽回的事实。”<br />
<br />
希望之声国际广播电台记者方亮采访报导。</div>

							
							
							
							
