													<h2>张伟国详解“万里谈话” 中共已经无可救药</h2>
						
						
																											<div id="postmessage_207195" class="t_msgfont"><i>张伟国，首发“ 老同志讲话” 的《新世纪》网站主编</i><br />
<br />
<a href="http://www.aboluowang.com/news/data/2009/0814/article_83658.html" target="_blank">http://www.aboluowang.com/news/data/2009/0814/article_83658.html</a><br />
<br />
【 阿波罗新闻网2009-08-14讯】&nbsp;&nbsp;<br />
　<br />
<br />
2009年8月13日 星期四&nbsp; &nbsp;&nbsp;&nbsp;节目长度：5分37秒 下载mp3上（ <a href="http://media.soundofhope.org/audio01/2009/8/13/zhuanfang1.mp3" target="_blank">http://media.soundofhope.org/audio01/2009/8/13/zhuanfang1.mp3</a> ）<br />
<br />
日前一篇题为“执政党必须要建立基本的政治伦理”的文章在网上热传，据称是根据中共党内一名曾居最高领导层的元老同中央党校一名年轻教授的四次谈话整理而成，并经谈话人审定。海内外专家和网友推断谈话者是前中共高层元老万里(曾任国务院副总理、中央政治局委员、中央书记处书记、全国人大常委会委员长)，本台就此专访了《动向》杂志和《新世纪》网站主编张伟国先生。<br />
<br />
张伟国先生告诉本台记者，这篇谈话是北京有身份的人委托新世纪网站首发的，但委托人并未透露谈话者的真实身份。该文在网上热传后，引发多方关注和猜测。张伟国表示，他接触到的圈内人士透露，谈话者是万里，海内外知识界绝大多数人也都同样认为。<br />
<br />
（声音）“发表以后，有不少评论都认为这个可能是万里先生的一个讲话，我想他们都会有自己观察的依据和角度。万里在中国共产党内部是一个比较旗帜鲜明的改革派，而且在高层领导里面也是一直关注知识分子，推动改革，尤其是希望决策的科学化、民主化这样一位元老。从他的行为和思想逻辑推演下来发表这样一个讲话，我想也是顺理成章的了。”<br />
<br />
张伟国分析了这篇谈话没有署名的原因。<br />
<br />
（声音）“作者不愿意把名字署出来，他也有他比较深刻的蕴意，因为现在我们读过这篇文章，应该很清楚就是中国社会，中国共产党所存在的问题的严重性，远远超过这篇文章是不是署名的严重性。到底是谁写的并不重要，重要的是他讲的这些内容，在中国社会严重，足以引起人们的注意的。”<br />
<br />
张伟国认为，这篇谈话突破了中共的底线，对中共进行的分析和评论振聋发聩，分析中肯、深入、直指要害。他分析说中共这个专制机器具有强大的同化能力，熟悉中共历史的人都知道，中共网罗了中国社会的许多精英和豪杰，但是都把他们一个个改造成了中共专制机器上的螺丝钉和部件。<br />
<br />
（声音）“比如，远的象周恩来这样的，人中豪杰了，相当聪明，相当有才华，有智慧。但是他的整个一生，我们看到他为这个专制机器所做的这一切，事实证明他完全被同化掉了。近的象朱熔基，他上台的时候，大家也是对他倍加青睐，把很多希望都寄托在他身上，结果看到他的家属非常快速的的就加入到这个腐败集团里面，成为共产党这个既得利益集团的重要成员，而且维护这个既得利益集团，作出巨大的贡献。所以象这样的一种机器的特性，象胡耀帮、Zhao Ziyang不被同化，保持自己的本色，最后的结果只有被打倒。在这种情况下，象现在的这位老同志出来讲话，可以看出他既没有被同化掉，也没有象胡赵这样被打倒，所以整个讲话的风格、立足点，几乎就是在这中间的一种平衡。一方面他要把没有被同化的良知表达出来，所以他从政治伦理的角度对共产党的意识形态，党文化最基础的东西提出质疑，非常发人深省，应该讲中共建政六十年来，所看到的公开的文章里，这篇是最尖锐，最击中要害的。”<br />
<br />
但是这篇谈话使用的是中共的那套语言方式，张伟国说：（声音）“另一方面，因为他没有被打倒，没有象胡赵这样的一种下场，从某种角度讲，他还是在这个体制既得利益集团的一个成员，他也就需要用他们的语言，他们的表达方式来表达自己的看法，所以这个讲话在外界看来也是比较温和啊，不够泼辣，不够直率，我想这是他的角色，他的位置决定的。”<br />
<br />
