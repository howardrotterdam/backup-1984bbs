<br />
近来，一篇谈政治伦理的文章在网上流传，据传作者是一位已退休的党和国家领导人。由于「作者」身份特殊，文章很快引起一些炒作。一些故知向这位老领导和他的家人提起这篇文章，得到的回答却是，根本没有这回事，文章完全是编造的。<br />
<br />
在这篇洋洋洒洒近万字的文章中，伪作者刻意模仿一位领导人的口吻，看来是下了很大功夫。但既然是谈伦理，就应该讲道德，冒用他人名义公开发表文章，无论从哪里个角度看，都是不道德的。<br />
<br />
过去，冒用他人名义并不容易得逞，可在互联网时代，匿名的特性既让发言者获得了自由的空间，也给生事者留下了造谣的空子。每一个人都可以通过博客等形式发布信息，真真假假让人难以辨别。最近几年，因网上造谣诽谤引起的官司屡见报端。前段时间，有人在网上造谣说出现了钴泄漏，吓得河南杞县几十万人大逃离，更让人领教了网络谣言的威力，因此，对此类谣言的危害不可小视，需要及时揭露以正视听。<br />
<br />
在这篇文章中，「多党竞选」、「军队国家化」、「废除共产党的领导」这些老偏方装进了「政治伦理」的新瓶子，看了的确让人眼花。可这些「营销策略」并没有掩盖住其真实作者对中国政治伦理认识的浅薄。<br />
<br />
所谓政治伦理，大致说的是政治活动中的道德以及政治与道德的关系。古今中外，大贤大哲对此多有阐述，但不同时代、不同国家、不同文化背景对政治伦理的认识也各不相同。在中国古代，孔孟主张仁政，韩非主张严刑峻法；在古希腊，柏拉图主张哲学家治国；后来，马基雅维利主张君主不必讲究信义，要像狮子一样凶猛，像狐狸一样狡猾；洛克主张君主立宪；卢梭主张人人平等，以人民自由意志所订立的社会契约作为统治基础。可见，对何为政治伦理、一国应以何种政治伦理为圭臬，历史上并无定论。一个国家只能从自己的国情出发，确立适合本国的政治伦理。<br />
<br />
在现今中国，政治伦理的核心就是「以人为本」、「执政为民」。它是中国古代民本思想的纵坐标与尊重生命权利世界潮流的横坐标交汇之处，即体现了中国特色，也符合世界的发展潮流。在这一政治伦理指导下，执政党一切行为的出发点，就是让老百姓过上好日子。过去三十年的历史证明，中国特色社会主义道路能让中国老百姓越来越富裕，执政党就必然会坚持不渝地走下去，而西方式民主远不是放之四海而皆准，已经有许多国家和地区的事实证明了这一点，中国没有理由拿十三亿人民的幸福去冒险。继续走已经被证明的成功、繁荣、稳定之路，这才是正确和理性的选择。<br />
<br />
所谓一个老同志的谈话，让中国实行西方式民主可以说得轻松，但如果药方不灵怎么办，却没有交待。如果中国出现泰国式的政治乱局，如果中国像苏联一样四分五裂，该文作者是不会负责任的，因为他连名字都不敢留下，可中国的老百姓却要为这份没有签名的医嘱，承担无法想象的风险，这种责权的不对等，难道就是伪作者要传达的政治伦理吗？<br />
<br />
一个十三亿人口的国家要繁荣、要发展，不可能没有磕磕拌拌，不可能没有这样那样的失误。因此，中国未来的路，肯定需要在政治经济体制上不断改进，中国共产党也会有容人的雅量，多听取各界对执政者提出宝贵意见。提意见的，如果真为国家百姓计，也应该讲点儿伦理，最起码，要光明正大，不要鬼鬼祟祟假借别人名义作此等文章。</div>

							
							
							
							
