													<h2>《老同志的讲话》是伪造的？</h2>
						
						
																											<div id="postmessage_207045" class="t_msgfont">以下为voa的新闻：<br />
网址：<a href="http://www.voanews.com/chinese/2009-08-16-voa23.cfm" target="_blank">http://www.voanews.com/chinese/2009-08-16-voa23.cfm</a><br />
<br />
北京正式表态，抨击网上流传的所谓《老同志的讲话》是“伪造出来的伦理”。一份据说是前“党和国家领导人”的万字讲话在中文互联网上广泛流传。有北京观察人士说，当局高调批判“老同志”的这篇讲话，说明其的确有可取之处。<br />
<br />
中通社周末发表署名“言力”的文章，题目是《伪造出来的伦理》。文章批判了近日在中文网上流传的一篇题目为《国庆60周年前夕一位老同志的讲话》的文章。言力文章说，“一些故知向这位老领导和他的家人提起这篇文章，得到的回答却是，根本没有这回事，文章完全是编造的。”<br />
<br />
对于网上流传的形形色色各式各样的文章，北京一般很少正式发表评论或出面澄清、展开批判。<br />
<br />
*文章未点作者真名 何以调查核实？*<br />
<br />
北京资深新闻工作者李大同很早就读到了这篇文章。他对美国之音说，这篇文章根本没有提到作者是何人，并没点名，当局又如何能“向老领导和家人”展开调查？李大同说：“问题是这个老领导没人说是谁啊！你怎么让‘故知’去核实呢？网上传是万里，没人说是万里啊！这篇谈话中哪里说到是万里了？”<br />
<br />
万里是中共元老，属邓小平重用的改革派，80年代初，Zhao Ziyang在四川、万里在安徽当第一把手，率先鼓吹和进行改革，解决农民温饱问题。后有民谣说：“要吃粮，找紫阳，要吃米，找万里。”<br />
<br />
这篇文章一出来，就有不少言论猜测是万里的讲话。万里已经93岁，早已退休。前几天邓小平遗孀卓琳去世，万里曾去邓家吊唁。<br />
<br />
香港苹果日报曾援引北京评论人士的话说，这篇文章，即便不是出自万里之口，也是其他人如乔石、田纪云、或谷牧之口。<br />
<br />
*“老同志”讲话的核心内容*<br />
<br />
不过，这位“老同志”的谈话主要说到了执政党的问题：“我们有7千多万党员，但这个党至今还没有在社团管理部门登记过”；“我们的国家还没有现代意义上的政党制度”；“国家还是党的国家”；“党军一体没有被国家对军队的领导代替”；“党内没有建立起真正意义上的竞争性选举制度，更不用说在国家范围”。<br />
<br />
另外，谈话还说到了宣传部门的问题和宣传手段问题：“政治宣传离事实太远，是野蛮的宣传”。谈话还提到了执政党反思问题：“执政了 60年，总应有起码的反思勇气吧。”谈话也提到不要封杀不同声音：“政治上不同声音“尤其应该好好听听，封杀不得”；“绝对不能用类似手段(指舆论封杀、肉体捕杀)来对待不同意见”；“民意处理失误，是我们(中共)最的大失误”。<br />
<br />
中通社发表的言力的文章说，“冒用他人名义公开发表文章，无论从哪个角度看，都是不道德的。”<br />
<br />
*“元老”讲话可能是“后生”所为*<br />
<br />
<br />
北京资深新闻工作者高瑜认为，这篇文章不大可能出自中共元老之手(之口)。高瑜说，这些老人均已“老迈年高”，其分析问题能力、深度和高度已经达不到“一个老同志的谈话”这样的标准。高瑜认为，这篇“元老”文章可能是“后生”的杰作。<br />
<br />
言力文章还说，在“老同志”这篇文章中，“多党竞选”、“军队国家化”、“废除共产党的领导”这些老偏方装进了新瓶子，看了的确让人眼花。可这些“行销策略”并没有掩盖住其真实作者对中国政治伦理认识的浅薄”。<br />
<br />
*“老同志讲话”有废除共产党？*<br />
<br />
不过，前中青报冰点周刊主编李大同说，“老同志”文章真假不论，就其内容来说，言力文章说，老同志文章提到“废除共产党”领导，云云，这根本就不是事实。<br />
<br />
李大同说：“没有！他哪里有讲到废除共产党领导啊！？当然没有啦。他只是说，你要允许别人来竞争嘛！”<br />
<br />
*“老同志”观点都是共产党早先提出的*<br />
<br />
李大同说，“老同志”文章通篇没提废除二字；至于其他一些军队国家化、多党竞争等观点也都有出处，都是共产党以前讲过的。他说：“这些观念，恰好就是中国共产党自己的观念。多党选举，人民有选票，军队国家化。所有这些，在中国历史上都有文章，社论，领导人讲话，不断提出过。”<br />
<br />
李大同说，这些观念，在90年代中期曾汇编成一本《历史的先声》。他说：“ 中共在半个多世纪前，在执政前，全部都是阐述的这些理论”；“今天执政党故意把这些都忘掉了”。而“老同志”没有说错，他在重申这些当年共产党不断强调的理论和主张。<br />
<br />
*谁为讲话后果买单？*<br />
<br />
言力的文章还说，“老同志”讲话让中国实行西方式民主可以说得轻松，但如果药方不灵怎么办，却没有交代。文章说，“如果中国出现泰国式的政治乱局，如果中国像苏联一样四分五裂，该文作者是不会负责任的，因为他连名字都不敢留下，可中国的老百姓却要为这份没有签名的医嘱承担无法想像的风险，这种责权的不对等难道就是伪作者要传达的政治伦理吗？”<br />
<br />
北京资深新闻工作者高瑜也认为，“老同志”文章，在这方面也许考虑得不那么周全，但是当局发表这样的文章，则更是借这个机会来封住大家的口。她说：“这点肯定有问题。中国现在连言论自由、结社自由都没有，还有什么风险不风险？！它(当局)就是以此为借口来阻碍政治民主化。”<br />
<br />
*政治敏感文章内外有别*<br />
<br />
在海外互联网谷歌搜索引擎上，输入关键词《一位老同志的谈话》，可查到31万8千条，而输入中国百度网则一条也差不到。百度通知上说：搜索结果可能不符合相关法律法规和政策，未予显示。这表明中国大陆以将此“老同志”的文章封杀。</div>

							
							
							
							
