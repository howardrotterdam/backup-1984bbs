													<h2>TG开始组织火力回击老同志讲话</h2>
						
						
																											<div id="postmessage_209055" class="t_msgfont">混乱的思维逻辑 错误的政治判断<br />
&nbsp; &nbsp;香港中通社八月十七日发表署名为任永清的特稿，标题为《混乱的思维逻辑 错误的政治判断――读所谓“一位老同志的谈话”有感》，全文如下：<br />
<br />
　　最近，海外媒体上流传一份自称曾经“享受着很高的政治待遇”的中国共产党“高级领导干部”的“一位老同志”题为《执政党要建立基本的政治伦理》的谈话稿，此文是真是假？这位“老同志”是谁？猜测种种，判说纷纭。其实，猜测与考证所谓的“老同志”身份及其言论的真伪，并不当紧，最需要加以注意的是这篇“谈话”流传出来的时机和意图。<br />
<br />
　　标题是文章的灵魂。这篇谈话的副标题是――“国庆六十周年前夕一位老同志的谈话”。明眼人一看便知，此文是冲着中华人民共和国建国六十周年这件大事来的。用这位“老同志”的话来讲，就是要在国庆六十周年前，“概括”、“总结”、“向老百姓说清楚”由中国共产党领导的国家，六十年取得的成绩是不是辉煌的。所谓“一位老同志谈话”用了很多类似“说不清楚”、“讲不明白”、“有疑问”、可以“好好总结，好好讨论”这样一些含混的言辞，提出作为执政党的中国共产党在建国六十周年到来之际，需要做的事情应当是“举国反思，举党反思”。而这位“老同志”经过“反思”后毫不隐晦地表示，由于共和国历史上曾经发生过“大跃进”、“文化大革命”这样的曲折，因此共和国“辉煌六十年”的提法是“不符合事实”的、是“站不住脚”的。此番言论出自自称曾经位居要职的“一位老同志”之口，着实让人匪夷所思。对于新中国六十年是否辉煌，难道还需要我们费过多的笔墨来加以描绘吗？！因为全世界各种媒体早已赶在我们之前，用或高兴或羡慕或嫉妒的言辞来评论中国的六十年了：六十年间，中国政府的政策帮助数以亿计的百姓脱贫致富、获得温饱；六十年间，香港、澳门顺利回归，显示了中国共产党在国力强盛和政策正确的前提下对整个中华民族的吸引力和凝聚力；六十年间，载人航天成功、奥运盛会成功成为中国崛起的标志；六十年间，中国走过了其它发达国家用了上百年、甚至几百年才能走完的发展历程，尤其是全面的改革开放释放了蕴藏在中华民族体内的巨大能量，创造了“东方奇迹”、“中国模式”……有的海外舆论甚至断言，这六十年是中国历史上最值得称颂的辉煌年代。这样的评价，来自持各种政见的媒体，不管它对中国共产党和中华人民共和国喜欢与否。而所谓“一位老同志谈话”却断言中国的六十年并不辉煌，这在世界舆论面前难道不是显得有点另类、有点不合时宜吗？当然，前进道路上没有一马平川，我们肯定共和国六十年的辉煌并不掩盖曾经有过的失误与曲折，因为任何一个国家走向进步，任何一个政党走向成熟，都不可避免遇到艰难险阻，更何况中国特色的社会主义是在前无古人的前提下建立和发展起来的，发生一些失误在所难免。中国共产党和中国人民毕竟走出了挫折与失误的阴影，创造出天翻地覆的奇迹。曾经的阴霾怎么能够盖得住今天的光明？中国六十年的成就是瑕不掩瑜的辉煌，值得高兴，值得庆祝。我们不妨环顾一下域外，记得美国建国一百周年时，美国从上到下一片兴高采烈，没见哪位煞风景的仁兄冒出来要政府和民众不要忙着庆祝，而应该先反思历次重大政治危机、经济危机的失误嘛。高谈“政治伦理”的所谓“老同志”，看来需要检点一下自己是否具有正常的思维逻辑。而利用所谓“老同志谈话”来达到否定中华人民共和国六十年成就的做法，在世界上也要属于“和者该寡”之类，恐怕要留下历史笑柄。<br />
<br />
　　所谓“一位老同志谈话”通篇围绕的中心，是对中国共产党六十年执政及今后继续执政是否合法的质疑。“谈话”认为中国共产党缺乏执政合法性的一个理由是，中国共产党“对过去为什么不做正确的事情连个起码的交代都没有”。“谈话”的杜撰者恐怕太健忘了。就在“一位老同志谈话”中提到的“决议”（即《关于建国以来党的若干历史问题的决议》）中，中国共产党对建国以来发生的重大失误作出了严肃的总结，承担了政治责任，包括对毛泽东同志的失误及其原因进行了深刻的分析。例如，在谈到“文化大革命”时，决议指出，“文化大革命”是一场“由领导者错误发动，被反革命集团利用，给党、国家和各族人民带来严重灾难的内乱”。“文化大革命”的历史，证明毛泽东同志发动“文化大革命”的主要论点“是完全错误的”，对此，“毛泽东同志负有主要责任”，对毛泽东同志的错误，党中央未能及时加以纠正，因此党中央领导集体也主动承担了责任。中国共产党如此坦率地承认错误承担责任，难道不是严肃的负责的交代吗？难道不是在向全党全国人民道歉吗？依我看，象中国共产党这样勇于面对失误、认真吸取教训的政党，在当今世界上恐怕并不多见。<br />
<br />
　　所谓“一位老同志谈话”指称中国共产党执政不具法理性的另一个理由，是中国共产党违背了、“推翻了”曾经在执政前对人民做出的“要建立一个民主、自由、独立的国家”的承诺。且不说毛泽东同志的著作中随处可以见到上述的有关论述，单在新中国宪法中就对此做出了明确的规定。如一九五四年宪法总纲第一条规定，“中华人民共和国是工人阶级领导的、以工农联盟为基础的人民民主国家。”二00四年宪法总纲第一条也规定，“中华人民共和国是工人阶级领导的、以工农联盟为基础的人民民主专政的社会主义国家。”可见，关于国家人民民主属性的表述始终没有改变。对于六十年间中国民主的进步，就连客观的国外媒体甚至一些西方政治家也都给予了不同程度的肯定，例如美国前总统卡特就高度肯定了中国基层的民主选举。指责中国共产党推翻了过去对于民主自由独立的承诺，这是从何说起呢？其思维的逻辑性又在哪里呢？<br />
<br />
　　所谓“一位老同志谈话”指称中国共产党执政不具法理性还有一个理由，即中国共产党忽视民意，“不对民意负责”。事实上，中国党政领导对民意的重视和尊重，与时俱进。例如，二00八年六月二十日，Hu Jintao主席在网络论坛里与网民交流。胡主席说，通过网络能知道人民群众所思所想，能聚集所有人的智慧，所以网络是一个重要的渠道。英国BBC对此报道称，胡主席与网友直接交流体现了中国共产党对民声民意的重视。外国媒体和政界普遍注意到，问政网络，近年也已成为中国一些地方政府的执政新风。在诸如重庆最牛钉子户、山西黑砖窑案、厦门PX项目缓建、华南虎照片风波等一系列事件中，网络舆论对事件走向产生了巨大影响，草根网民聚合而成的强大力量得以体现，亿万网民成功实现了意见表达、政治参与。对此，“老同志谈话”怎么可以视而不见呢？<br />
<br />
　　现在我们来看一看所谓“一位老同志谈话”流传出来究竟要达到什么目的。这篇谈话使用的核心词语是“政治伦理”，出现频率最高的数字是六十。全篇谈话指责中共执政六十年太长久，这么长期的执政，不符合“政治伦理”。把这句话说白了，就是共产党已经执政六十年了，继续执政下去就“不道德”了。那么由谁来执政才符合“政治伦理”、才“道德”呢？“老同志谈话”中有一段话一语道破：“六十年了，我们国家没有成长起应该有的社会性力量来与我们共产党竞争，来提醒、来监督我们党……这种现象是很不伦理的。”原来是要求让与共产党执政的同时参政并拥护共产党的八个民主党派起来“竞争”（其实“老同志谈话”真正想表达的意思是“抗争”），让所谓“社会性力量”成长，与共产党轮流执政，那才符合“政治伦理”，那才“道德”！那不就是在鼓吹西方的“多党制”政治吗？！可见“老同志谈话”的要害，是否定中国的根本政治制度人民代表大会制度，否定中国共产党领导的多党合作和政治协商的制度，卖力兜售西方政治体制。这同前不久达赖在国外所谓的“中国共产党执政快六十年了，该退休了”的言论不是如出一辙吗？<br />
<br />
　　其实，一个政党执政的基础在多数民众的认同与支持，在正常情况下，执政时间越长，说明其执政基础越稳固。因为执政时间长，所以就失去继续执政的法理基础的说法，是与政治常识及正常的思维逻辑背道而驰的，只能反映出所谓“一位老同志谈话”在思维方式上的混乱。<br />
<br />
　　再过一个多月，中华人民共和国成立六十周年国庆节就要到来，中国人民正以无比自豪的心情积极筹备庆祝活动。国内外有些人难免要利用这样的机会搞点小动作。但是，我想提醒这些人，有正确的思维逻辑才可能作出正确的政治判断，在这种喜庆的当口，谁要是不识时务，哗众取宠，悖逆民意，硬往七千六百万中国共产党党员和十三亿中国人民头上泼脏水、冷水，扫那么多人的兴致，甚至通过指责中共现行政治制度、政党制度和方针政策来离间执政党和老百姓的关系，试图挑起什么事端，恐怕只会冷落了自己、孤立了自己，讨一个大大的没趣。</div>

							
							
							
							
