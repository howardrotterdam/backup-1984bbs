													<h2>刘渠景 ：请问成龙先生，中国人需要被谁管？</h2>
						
						
																											<div id="postmessage_664144" class="t_msgfont">　　应邀出席博鳌亚洲论坛开幕式的香港艺人成龙先生在回答外国媒体提出的关于文化自由的问题时这样说道：“有自由好还是没有自由好……真的我们现在已经混乱了、太自由了，就变成像香港现在这个样子很乱，而且变成台湾这个样子也很乱，我慢慢觉得，我们中国人是需要管的。” <br />
<br />
　　我不知道成龙先生这里所说的“太自由了”是什么意思，更不知道其所说的“我们中国人是需要管的”是什么意思。我们中国人与外国人相比真的是太自由了吗？我们中国人又需要被谁管？莫非成龙先生认为我们中国人还需要外国人来管我们？ <br />
<br />
　　成龙先生作为具有国际影响力的明星，其一言一行的影响力非普通人可比，而其在回答媒体的相关提问时却抱怨我们中国人的自由太多以至于需要被管，如此荒唐可笑的言论恐怕也要与其国际影响力一起蛮声海内外了。 <br />
<br />
　　当然我们不能从成龙先生言论的字面意思来推断成龙是希望让外国人来管理我们中国人，那样可能就冤枉他了。但除此之外，我实在想象不出成龙先生所说的“我们中国人是需要被管的”是什么意思。莫非成龙先生的言下之意是说中国人都是调皮捣蛋的孩子，需要被管？那么如果不是被外国人管，又被谁管？又或者成龙先生的意思是一部分中国人需要被另一部分中国人管？那么在成龙先生看来，哪部分中国人属于需要被管的？哪部分中国人又是有资格管人的？ <br />
<br />
　　笔者揣摩成龙先生的言外之意或许是：中国的民众需要政府来管。也就是说，成龙先生所说的“我们中国人”可能只是指中国的民众，而所谓“我们中国人是需要被管的”则可能是说中国的民众需要政府及其官员来管。进一步说则是，官要管民，要像大人管孩子一样，不能使其太自由，使其任着自己的性子“胡闹”。 <br />
<br />
　　但我们知道，政府及其官员与公民的关系并非如同家长与孩子的关系那样属于管与被管的关系，而是主人与公仆的关系，也就是说，人民、公民才是国家的主人，政府及其官员则是公仆，是为人民服务的，是为公民与公共利益服务的。go-vern-ment及其官员依照人民制定的法律依法行政，依法为公民与公共利益服务，其本身并没有管理公民、管理中国人的权利。 <br />
<br />
　　作为人民公仆的政府及其官员可以依照作为国家主人的人民所制定的法律管理公共事务与社会事务，但其这里的管理所针对的是公共与社会事务，是管理“事务”，而不是管理“人”，不是管理中国人。中国公民在法律范围内享有一切自由。这种自由是每个人都渴望和追求的，包括成龙先生本人。一个正常人完全具有法律赋予的一切自由，不再像孩子那样还需要他人来管。 <br />
<br />
　　可见，我们中国公民在法律上是完全自由的存在，而且我想中国人也完全具有行使自由权利的能力。因此中国人无需外国人来管，也无需社会的一部分人来管另一部分人。而政府基于法律赋予的权力的管理也仅仅是针对社会事务而非公民个人。作为公民个体，我们每个人的意志都是自由的，如果一个国家的人来管另一个国家的人，那么这就是民族之间、是国家之间的压迫与奴役，如果是一个国家中的一部分人管另一部分人，那么这就是社会压迫与奴役。 <br />
<br />
　　在现代中国，无论是国家间、民族间的压迫与奴役还是一个国家内部的社会压迫与奴役都已经基本上不存在了。我们“中国人民已经站起来了”，当家作主了，不再受人奴役了。但我们的成龙先生好像对这种自由生活很不适应，竟抱怨起来，竟“慢慢觉得，我们中国人是需要被管的”。那么请问成龙先生，你这话代表的是哪些中国人，你又想被谁管呢？</div>

							
							
							
							
