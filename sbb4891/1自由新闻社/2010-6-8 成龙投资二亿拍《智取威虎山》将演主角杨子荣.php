													<h2>成龙投资二亿拍《智取威虎山》将演主角杨子荣</h2>
						
						
																											<div id="postmessage_663808" class="t_msgfont">东方日报6月8日报道 成龙将投资开拍内地八大经典样板戏的《智取威虎山》，并由徐小明执导，投资约三千万美元，即超过二亿元人民币，并准备以3D制作。而导演徐小明透露此片的详情，表示成龙为了此片不惜把原本给好莱坞电影的档期提早给此片开拍，宁愿延迟好莱坞片的拍摄期，显示他对此片之重视。<br />
<br />
亲到台湾选角<br />
<br />
<br />
徐小明表示会在本月中的上海电影节，在大陆、香港、台湾三地合作趋势的论坛中介绍《智取威虎山》，作为陆、港、台三地合作电影，他们会与八一电影厂合作，成龙会到台湾选角，更会饰演《智》片男主角“杨子荣”。<br />
<br />
另外，徐小明透露较早前已往东北选场景，稍后会往加拿大及韩国，以找地形险要的大雪山配合拍摄。由于《智》片涉及大量动作场面，例如雪崩、雪地大战等，而现在不少制作都在拍摄中加上3D效果，徐小明透露他们也有此打算，成龙更以一句：“我们向3D的目标进发。”徐小明表示若以3D制作，费用会更为高昂，但因为戏中场面浩大，又有不少特技，故绝对值得投资。<br />
<br />
(本文来源：东方日报 )</div>

							
							
							
							
