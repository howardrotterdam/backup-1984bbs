<br />
　　当记者问为什么要练“瞪眼”时，他们的理由很简单：“眼睛就像名片，眼神决定了一个人的精神状态”，“训练的目的是让眼睛更有神，让精神更饱满，以最好的状态接受党和人民检阅”。<br />
<br />
　　报道说，他们最初跟其他人一样，坚持不了一分钟就得眨眨眼。后来，他们下起了苦功夫：每天早起半个小时练瞪眼，这还不够，为提高眼睛抗干扰能力，他们干脆迎着风向看、向着太阳看……哪里干扰物多往哪里看。<br />
<br />
　　<strong>李涛透露：“起初，眼睛经常不自觉地流泪，流出的慢慢风干，最后泪腺干了，就不流了，期间眼睛始终保持一眨不眨，也挺难受。”</strong><br />
<br />
　　不过他们最终挺过来了。从最初的一分钟，一直到30分钟、40分钟、50分钟……瞪眼时间一点点延长，功夫越来越高。<br />
<br />
　　<strong>他们的阅兵方队不久前组织了一次“瞪眼”比武，两人出尽风头，比武开始还不到五分钟，各路人马已纷纷退出，变成李涛和郭东旺的“巅峰对决”。在同僚的加油声中，时间一秒秒过去，两人的成绩最终锁定在70分钟，双双荣膺“瞪眼王”称号。</strong><br />
