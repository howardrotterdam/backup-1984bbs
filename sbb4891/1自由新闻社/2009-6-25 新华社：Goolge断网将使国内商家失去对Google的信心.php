　 昨天晚间，记者登陆Google搜索引擎，发现已经无法正常使用。随即展开的调查显示，此次故障波及很广，记者所能接触到的来自全国各自的网民均都表示不能正常访问。<br />
<br />
　　根据调查，记者发现，此次故障是大约从昨天下午9点左右开始的。当时利用Google进行搜索的网民随即发现不能正常使用。登陆网站也被发现无法实现。另外Google公司的其他服务也相继出现了无法访问的情况。<br />
<br />
　　记者在当晚也试图联系国内两大搜索引擎服务商。其中一家对记者表示，他们不能对此情况并不熟悉，不能做出表态，或者是推测。而记者联系各大网络了运营商的负责人时，他们表示国内网络包括中国与国际互联网之间的接口完全正常。<br />
<br />
　　当天晚上，也有部分搜索技术的研究人员在追踪这件事情的发展。根据他们的推测，认为不排除Google的临时工雇员导致了技术故障或其他方面的原因。<br />
<br />
　　面对此次Google大面积的服务器瘫痪事件，我们第一时间联系了国家计算机网络中心，计算机网络中心的胡专家表示：“最近一段时间气候比较异常，Google服务器一般没有应急措施，致使大面积的服务器瘫痪。从这次事件中，我们应该汲取教训，进一步加强防范意识，不让此类事件再次发生”。<br />
<br />
　　就网民可能所造成的损失，相关人士表示无法做出估量。除去影响内地网民的正常搜索外，上海一与Google有业务往来的商家也表示说，此次故障让他很多大单不能提交。<br />
<br />
　　能够被预测的到，则是这次故障将极大地削弱Google进入内地市场的步伐。在此之前有媒体报道称，有内地网络服务商在南方召开分销google广告业务的会议，并取得了出乎意料的效果。而这次故障，“肯定将使国内商家消除迷信，甚至失去对Google的信心。” <br />
