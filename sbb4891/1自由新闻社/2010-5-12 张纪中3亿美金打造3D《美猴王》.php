　　据悉，影片第一部将在今年筹备完成，明年拍摄，2012年全球上映。为此已筹备了两年，邀请了包括打造《指环王》《阿凡达》的维塔工作室等顶尖制作团队来中国考察，在看到《美猴王》的创意短片和构思素材之后，都表达了极大的合作兴趣。张纪中表示，《美猴王》将由真人演绎，演员包括中国和好莱坞明星，而导筒则将完全交给好莱坞的著名导演。<br />
<br />
　　据悉，目前《美猴王》剧组已经完成第一稿，耗资上百万美金。第二稿的创作也即将开始。电影《美猴王》将以三部曲的形式进行拍摄制作。三部有一个共同的大主题，每部都各自有不同的小主题。张纪中表示，在电影中展现“西游记”这种庞大的题材不一定按照小说的次序表达，电影将选取最典型的内容，以最简单的表达方式来展现深邃主题。而在合作上，张纪中透露，故事的脉络和走向由中国的编剧确定，未来电影形态的确立，适合于电影展现的剧情，则由美国编剧完成。张纪中说：“《美猴王》是一部中英文电影，走的是全球化市场，剧本不能是我们完全写好了去翻译，而是要融入西方电影结构的概念与创作。”</div>

							
							
							
							
