　　本报讯 （记者 卫昕） 昨日，3D、IMAX魔幻大片《美猴王》三部曲在北京召开新闻发布会，正式宣布全球启动。张纪中担任《美猴王》总制片人，他表示该片将与全球顶尖团队合作，3亿美金打造三部3D魔幻立体大片，通过获得全球市场的认可，使中国文化走向全世界。为此该片将邀请好莱坞大牌导演和演员加盟。<br />
