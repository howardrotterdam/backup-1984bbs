													<h2>朝鲜军方实施宵禁</h2>
						
						
																											<div id="postmessage_355526" class="t_msgfont">韩媒:朝鲜更换货币引发民众抗议 军方实施宵禁<br />
2009-12-04 07:08:00　来源: 人民网(北京)　 <br />
核心提示：朝鲜近日推行货币兑换引发争议，美媒3日称，由于遭到一些民众的抗议，原定1日开始的新旧货币兑换工作2日得以进行。韩国媒体报道，朝鲜当局还将每户可兑换10万旧币上调至15万。由于朝鲜居民不积极兑换新币，军队部门为了防止发生紧急事态，正在大幅强化警备力量。<br />
<br />
<br />
<br />
<br />
12月1日，韩国板门店一侧的韩国游客正在一块展板前看新出的朝鲜货币。据朝鲜官员当日对外透露，朝鲜于11月30日更换了货币，这是该国十七年来首次更换货币。新旧货币兑换率为1比100。<br />
<br />
人民网12月4日报道 美国媒体3日称，由于遭到一些民众的抗议，朝鲜原定1日开始的新旧货币兑换工作推迟到2日才得以进行。各地银行的兑换活动将持续到6日，新币将从7日起在市场流通。<br />
<br />
另据韩国媒体3日报道，由于遭到民众抗议，朝鲜当局还将每户可兑换10万旧币上调至15万。韩联社还称，朝鲜虽然从1日开始实施了货币改革，但朝鲜居民却不积极兑换新币，军队部门为了防止发生紧急事态，正在大幅强化警备力量。<br />
<br />
导致物价暴涨<br />
<br />
美国媒体3日援引韩国对朝援助组织消息人士的话报道称，朝鲜各银行2日上午正式开始新旧货币兑换工作，这一举措将使民众通过市场活动所积累的大部分私人储蓄化为乌有。<br />
<br />
报道称，据来自朝鲜内部的消息人士透露，货币兑换原计划1日开始，但由于事出突然，遭到了一些民众的抗议。朝鲜国内电话于11月30日被切断，政府下令实行宵禁以消除隐患，并将货币兑换工作推迟一天。<br />
<br />
农村的情况相对更复杂。据位于韩国首尔的一家与朝鲜有关的电台报道，一些朝鲜城市富人11月30日涌到农村地区大采购，希望在农村居民得知货币兑换消息之前花掉旧币。<br />
<br />
该电台还报道称，由于人们试图在大限之前花掉过多的朝鲜旧币，导致朝鲜境内大宗商品价格暴涨：自上周末以来，以旧币计的大米价格涨了20倍，玉米价格涨了30倍。<br />
<br />
居民公开表现愤怒<br />
<br />
尽管如此，朝鲜官方通讯社、电视台和主要报纸截至2日晚仍未对外宣布货币兑换事宜。据称，位于韩国和日本的有关组织一直在对朝鲜媒体进行监测。<br />
<br />
美国媒体报道称，朝鲜有关部门通过闭路系统发布消息，并把消息通过安装在家庭和街道上的喇叭传递。以这种方式发布的信息难以被朝鲜以外的地区监测到。<br />
<br />
朝鲜官方媒体的表现进一步加剧了此次行动的神秘感。此前，朝鲜曾四次发行新币，每次都声势浩大、对外界作出解释。<br />
<br />
在韩国的朝鲜人组织声称，在此次新旧货币兑换之前，朝鲜中等水平的家庭一般拥有100万元以上的现金，10万元只够一个四口之家生活两个月。<br />
<br />
报道还援引朝鲜内部消息人士的话说，政府下令将超过人均兑换限额的资金存入银行，但储蓄额也设置了30万到300万元的限制。另外，朝鲜银行属于“强制储蓄所”，存款人无法随时取钱，因此不少人发出了抱怨。<br />
<br />
该消息人士透露，虽然保安人员大幅加强了管制，但朝鲜居民仍公开表现出愤怒情绪。<br />
<br />
向黑市“宣战”<br />
<br />
韩国媒体认为，朝鲜政府此次推行货币改革措施实际上是向黑市“宣战”。此前，朝鲜黑市上出售的货物包括军需物资、煤炭、石油、机械等非法商品。自2005年10月开始推行粮食专卖制度以后，市场上交易最活跃的商品就是禁止私人买卖的粮食。对工业品实施的定价制也没有商店遵守。<br />
<br />
韩联社3日报道称，朝鲜居民们被突如其来的货币改革消息所震撼，不积极参与行动。多数朝鲜贸易商表示，目前，朝鲜虽然表面看起来很平静，但大多数居民认为好不容易挣来的钱却要当废纸使用，内心充满了不满的情绪。虽然当局宣布6日以后禁止使用旧币，但交换新币的朝鲜居民却寥寥无几，朝鲜中央银行各地储蓄所也门可罗雀。目前，虽然货币交换作业已经过了两天，但在新义州很难见到新货币。<br />
<br />
据韩国媒体报道，中朝边境丹东口岸的对朝贸易目前基本停止，等待朝鲜情况稳定。<br />
<br />
<br />
中朝贸易大多采用美元或者人民币进行结算，朝鲜政府还没有公布新的外币兑换比例。此次货币改革前，朝鲜市场上1美元可以兑换3800朝元，1元人民币可以兑换597朝元。<br />
<br />
新闻回顾：朝鲜强推货币改革旧币100元兑1元<br />
<br />
12月1日起，朝鲜全面推行货币改革，将现有的100元改为新币1元。报道称，朝鲜推行货币改革，是1992年后时隔17年以来的首次。据透露，兑换货币的消息传出后，朝鲜人纷纷要兑换美元或人民币，引发了混乱。<br />
<br />
朝鲜推货币改革 商店停业长途车停运<br />
<br />
朝鲜自11月30日起停止使用原有货币，将更换新的货币。旧货币与新货币的兑换率为100比1，即100元旧货币兑换为1元新货币。韩国《中央日报》转引朝鲜媒体的报道说，在这一决定发出后，朝鲜市场当天出现了中断，并发生了大混乱。<br />
<br />
专家称朝鲜货币改革将损害富人利益<br />
<br />
11月30日，朝鲜“闪电”推行货币改革，将现有面值去掉两个零。重发货币之后，原来每张面值100朝鲜元的纸币现在将被换成1朝鲜元面值。平壤市内一些商店已经暂时停止营业，现在因为要等重新定价才能售货，估计要到6日以后恢复营业。<br />
<br />
(本文来源：人民网 )</div>

							
							
							
							
