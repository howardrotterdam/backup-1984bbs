													<h2>世界日报：英译“CHINA”一词必须弃用</h2>
						
						
																											<div id="postmessage_271612" class="t_msgfont">中新网9月24日电 菲律宾《世界日报》日前刊文《英译“中国”一词必须弃用》称，中国二字频繁出现在各种传媒，但中国为何英译为 China？推敲起来颇有些疑惑。China是西方人称呼中国的，西方人对中国一些地方是从他们的习惯或无知出发而称谓的，例如台湾曾被称为 Formosa，澳门又被称为 Macau，广州称为 Canton等。外国人叫的，大都改过来了。中国的地方就应尊重中国习惯，用本土名称，呼吁摒弃China，而改用汉字拼音 Zhongguo。<br />
<br />
文章摘录如下:<br />
<br />
半个多世纪前，“中国”二字意味着穷又乱，令中外人士头痛不已；如今，中国却以小康大国之势崛起，全世界刮目相看。尤其是金融危机一发生，世界的眼光盯着中国，甚至希望中国伸出援手拉他们一把。<br />
<br />
中国二字频繁出现在各种传播媒体上。但回过头来，中国为何英译为 China？推敲起来，颇有些疑惑。查《现代高级英汉双解词典》及《朗文当代英汉双解词典》。此两词典可谓世界级权威，希望求解疑惑。但翻来翻去，希望变为失望。此两词典均只有小写 c 开头的 china，译为陶瓷、瓷器，而无大写 C 开头的 China！有大写 C 开头的 Chinese，译为中国人、中国话、中国文。<br />
<br />
至此，问题产生了。明明权威字典上没有 China( 请注意大写 C 开头)，而英文书报上凡是涉及中国的事，就出现 China 一字，难道是作者胡撰或是上述词典编者有意不收入 China 一字？究竟是谁在什么时候将 china 解为瓷器外，把中国也叫做 China？<br />
<br />
秦始皇于公元前221年统一中国。在这之前，诸侯割据，混战不断，一个小小的地方就是一国家，什么齐楚燕等都是。孔子在山东一游就叫“周游列国”，即为一左证。可见，由于没有一个统一的国家，也就没有一个统一的国名。近偶翻《礼记?礼运篇》中，有“中国”二字。秦始皇统一中国后，为何弃“中国”二字不用而用“秦”代表，恕不考证。<br />
<br />
秦始皇统一中国后，大刀阔斧兴利除弊、破旧立新，建成强大的秦国，疆域直达罗马，威名远播，连洋人也跟着叫“秦”。“秦”音近 chin，按西文习惯，字的后面加 a 即成名词 china，将前缀大写，成 China，隐隐约约有“秦”的缩影。<br />
<br />
另一方面，中国在商周时期(公元前1766到前221)就有上釉的陶制品，非常精美。通过陆上或海上交通输往欧亚国家，深受当地民众欢迎。因陶瓷品称 china，故民众也以陶瓷品产地称为 china 。以上两方面见解纯属笔者臆想，也有待学者论证。<br />
<br />
看来，China 是洋人称我们的。我们中国以前也只有朝代如清、元、明等称呼。事实上，洋人对中国一些地方是从他们的习惯或无知出发而称谓的。例如台湾曾被葡萄牙人称为 Formosa，澳门又被称为 Macau，广州称为 Canton，厦门称 Amoy 等。<br />
<br />
外国人叫的我们不认账，大都改过来了。中国的地方，就应该用中国的本土名称，道理再简单不过了。我们也按照中国的习惯，把外国一些地方“中国化”，例如旧金山、加州等等。但这些“中国化”的外国地名，只在华人中间流通，不具有法定地位。<br />
<br />
因此，中国就是中国，我们有理由摒弃洋人为我们取的 China，而改用汉字拼音 Zhongguo，中国人叫 Zhongguoes。中华则用 Zhonghua，这就名正言顺，而不需顺着洋人叫 China 了。总之，我们有十三亿人口，大约有七、八亿中国人在洋人面前听不懂自己国家的名字，你说问题严重不？时到今日，笔者呼吁“中国”二字的英译废弃。(杞桑)</div>

							
							
							
							
