&nbsp; &nbsp; 6月6日22：00时，天空灰暗，乌云密布，雾蒙蒙的，没有星星，没有月亮，也没有鸟的歌唱，只有偶尔远方传来几声鸟的凄凉的哀鸣。空气仿佛凝固了，外面的树枝、树叶一动不动，无精打采的样子，它们也得了肿瘤病吗？东明人的甲状腺肿瘤灾难感动上苍了吗？6月飞雪，举国同悲吗？<br />
&nbsp; &nbsp; 又要去北京了，收拾着用品，我的心在哭泣……<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 北京，是中华人民共和国的首都，政治、经济、文化中心，是一个中国人民骄傲和自豪的地方，是多少人的向往和梦想，是多少辈人的渴望和期盼！但是，我却不一样，我的家庭不一样，东明县的人民不一样，只因为，我是甲状腺肿瘤患者，我的弟弟、弟媳，我的妹妹，我亲爱的妈妈，全是甲状腺肿瘤患者，一个甲状腺肿瘤家庭，80%的东明县人民是甲状腺肿瘤患者，一个肿瘤县、癌症县。我！我的家庭，东明县人民对北京充满了恐惧和悲哀。我心在哭泣，泪在眼里打转，视线模糊。我现在已经是第四次去北京了，妈妈的甲状腺肿瘤做了3次手术，又扩散了，今年已经是70多岁的老人了，上了手术台能上下来吗？我弟弟做过两次手术又扩散了，弟媳做过一次手术，今天又扩散了，他们人到中年，正是事业有成的时候呀！妹妹是刚结婚不久，原来很庆幸自己没有甲状腺肿瘤，可在前几天单位体检时也发现自己长了甲状腺肿瘤，他们幸福的家庭还没来得及要孩子呀！我呢？我也是甲状腺肿瘤转移。天哪！离开东明――这片生我养我的热土，曾经无污染，碧水、蓝天、充满生机的小城镇。这，也许是永别！心里充满了茫然、绝望。别了，爹、娘、哥、姐，东明的父老乡亲，让我再看您们一眼，让我再多看你们一眼，看一眼这里熟悉的一切，这里的床、写字台、用惯了的钢笔、丈夫刚买了的粉红色的纱巾，这里的房子、这里的左邻右舍。我在房间里徘徊着，来回盲目的走动着。最后让我再看一眼熟睡的孩子，亲吻一下稚嫩的脸蛋，天真可爱的脸庞在睡梦中露出甜甜的笑，难道他长大也要遭此厄运吗？也要在脖子上开几道的口子吗？肚子上也要开几道口子吗？孩子呀！你们不知道，也许睡梦中醒来后，你已经没有了妈妈、没有了奶奶。妈妈呀，假如你下不了手术台，这，将是永别，再看一眼您的孙子吧！也许是最后一眼！您的孙子、孙女会记住您语重心长的嘱托和朴素的语言，“孩子呀，好好学习考走吧，咱家的空气难闻，水也难喝。要是我不在了，在远方的十字路口画个圈，烧把纸就行了，奶奶不怪你们….”苍天哪！我跪求您，把所有的肿瘤都给我一个人吧！假如，我的死能挽回全家人的平安幸福，能换回东明人民的健康快乐，我愿意立即跳楼自尽；假如，我的死能唤醒治理环境，减少化工污染，遏制东明县肿瘤的爆发，还给东明人民碧水、蓝天，我愿意立即触电而死。如果不及时治理，只是追求政绩、经济效益，生命没有了，健康没有了，人生还有什么意义？有什么意义呀！有什么意义呀！东明有多少个这样幸福的家庭将要分崩离析，要妻离子散呢？醒醒吧！少抽2包中华烟，少喝2瓶茅台、五粮液，关注一下民生吧！关注一下东明县的环境污染吧！<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;收拾好了去北京用的东西，我檫了一把脸上的泪水，强装高兴地说“妈，咱们走吧，去北京看看去，回来就好了。”，我故意骗我妈妈，说了这样一句善良的谎言，害怕她精神上支撑不了。就这样，我们一家五口人踏上了去北京的列车。东明的空气仍然沉闷、凝重、难闻，我要窒息了。<br />
&nbsp; &nbsp; 别了，东明――曾经美丽的家乡，碧水、蓝天、鲜花、芳草。<br />
&nbsp; &nbsp; 我浑身酸软，双腿颤抖，心在滴血。。。。。。<br />
