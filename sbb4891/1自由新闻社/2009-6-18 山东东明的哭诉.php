　　&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;五里铺北排水道排出，因会造成严重污染，遭到群众反对，县政府遂在小刘庄北买二白亩耕地让五里铺村搬迁，遭到村民 拒绝，县政府又确定把污水从工业路北端南下流经工业路中段再东转三八路西段直接进入了城区排污管网，然后流入县城东污水处理厂加以处理。此排污路线的两 旁，是包括十四所中小学的几万人的密集居民区。所埋排污管道是长度两米直径一米的水泥管连接而成，大部分接头多开放口多，渗漏污水严重。环己酮是用苯作原 料，再加入其它化学品，经过一系列的化学反应而生成的物质。在化学反应中生成的副产品和放出的气体是一种剧毒致癌物质，对人体危害特别严重。自古以来污水 都是从居民区排到郊外无人区，而现在却把有害污水从郊外排入居民区是何道理？<br />
<br />
　　&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;最近三年来，我县开始出现一种怪病，就是大部分人患了甲状腺肿瘤。得病人数从2008年开始发展特别快。2009年离退休干部查体时就新增加“查甲状腺肿 瘤”这个项目，据主检医生透露，患甲状腺肿瘤的人数已有百分之六十以上，照这样计算城区居民就有五六万患病，而且发展特别快，已有很多患者的病情恶变。例 如，县第一初中教师王君平家有12口大人，去年有六人患甲状腺肿瘤，今年再查已有9人患这种病；又如城关镇穆庄小学十个老师体检发现有六个患甲状腺肿瘤， 其中有一人因肿瘤较大，已做了切除手术。<br />
<br />
　　&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;在前几年环己酮厂还未建设前，我县没有一例甲状腺肿瘤患者！环己酮厂建设生产这5，6年来，甲状腺肿瘤病在我县突发，而且正以惊人的速度发展着。此状况如 果得不到有效控制治理，曾经人杰地灵的东明县很快就可能成为国家的新一个癌症大区。现在，我县几乎人人都怀疑甲状腺肿瘤是四个环己酮厂的污染引起的。我县 很多单位、团体代表多次向菏泽地区、县政府，人大，环保局反应这个问题，但始终没有得到解决。环己酮厂为了降低成本，仍然坚持把污水直接排到城区下水管道 中。<br />
<br />
　　&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;据反映这四个环己酮厂是部分县领导和某些大款合资建造的股份企业，姑受到了层层保护。他们不遵照科学发展观，只顾眼前小利，不管人民死活，广大群众对此极为愤慨！现在已纷纷签名上访。并准备申请成立自发的环保组织和上街$游$行$示$威。<br />
<br />
　　&nbsp; &nbsp; <br />
<br />
　　&nbsp; &nbsp; <br />
<br />
　　&nbsp; &nbsp; <br />
<br />
　　&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;【甲 状腺位于喉及上段气管两侧，由左右两个侧叶和峡部组成，峡部将两个腺叶连成一体，呈“H”形横跨于气管上段。正常情况下，在颈部是摸不着甲状腺的。甲状腺 是人体最大的内分泌器官，是唯一在细胞外储存其产物的内分泌腺。甲状腺是维持正常人体发育所必须的物质。它借助甲状腺素调节全身各组织器官的代谢活动及生 长发育等。<br />
<br />
　　&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;甲状腺肿瘤是颈部的慢性病变，好发于颈前部，形状似核桃，质地较硬，可随吞咽而上下活动。发病初期一般无明显症状，不痛不 痒，不影响说话、吃饭。所以，通常当人们体检时才被发现。但有的可短期迅速增大，颈部变粗，再继续发展可影响呼吸，少数可出现声音嘶哑或吞咽困难。此外， 它还可以继发甲亢，也可发生恶变。】<br />
<br />
　　&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;东明县洪业化工与东明县方明化工是同属方明集团的两家股份制私营化工企业。企业主要产品是环己酮。年生产能力分别是6万吨与1.5万吨。为追求利润的最大 化，企业开工建设之初就没有建造任何的环保设施。两家企业自投产以来，每天产生大量的含苯废水，洪业化工每天废水200吨，方明化工每天废水70吨。同时 两家化工企业每天产生大量的的二氧化碳及二氧化硫气体与整个菏泽市机动车尾气排放量相当，洪业化工每天的二氧化碳排放量为18万立方米，方明化工每天二氧 化碳排放量7万立方。如此大量的含苯废水，不经过任何处理，每天都要在凌晨12点左右，倾倒入方明化工厂区及洪业化工厂区周边的水沟内。大量的废水，严重 污染了东明县城区的地下水（方明化工厂区在县城区内，洪业化工在农村），致使其周边居民地下水无法饮用，大量的二氧化碳及二氧化硫严重污染大气。给整个城 市居民的用水安全及身体健康带来了极大的隐患。 <br />
<br />
　　&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;对于如此恶劣的行径，对于整个东明县尽人皆知的事实，东明县环保局及菏泽市环保局 从未采取过任何的措施。任其随意污染环境及地下水。对于两家机关如此行政不作为行为，作为一名普通百姓，表示强烈的抗议。对于以上情况绝对属实，我们希望 能够引起国家局领导的强烈重视，还我们蓝天白云，青山绿水！！！！！ <br />
<br />
　　&nbsp; &nbsp; <br />
<br />
　　&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;东明县一个甲状腺肿瘤患者家庭的<br />
<br />
　　&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;哭诉。。。<br />
<br />
　　&nbsp; &nbsp; <br />
<br />
　　&nbsp; &nbsp; <br />
<br />
　　&nbsp; &nbsp; <br />
<br />
　　&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;6月6日22：00时，天空灰暗，乌云密布，雾蒙蒙的，没有星星，没 有月亮，也没有鸟的歌唱，只有偶尔远方传来几声鸟的凄凉的哀鸣。空气仿佛凝固了，外面的树枝、树叶一动不动，无精打采的样子，它们也得了肿瘤病吗？东明人 的甲状腺肿瘤灾难感动上苍了吗？6月飞雪，举国同悲吗？<br />
<br />
　　&nbsp; &nbsp; 又要去北京了，收拾着用品，我的心在哭泣……<br />
<br />
　　&nbsp; &nbsp; <br />
<br />
　　&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;北京，是中华人民共和国的首 都，政治、经济、文化中心，是一个中国人民骄傲和自豪的地方，是多少人的向往和梦想，是多少辈人的渴望和期盼！但是，我却不一样，我的家庭不一样，东明县 的人民不一样，只因为，我是甲状腺肿瘤患者，我的弟弟、弟媳，我的妹妹，我亲爱的妈妈，全是甲状腺肿瘤患者，一个甲状腺肿瘤家庭，80%的东明县人民是甲 状腺肿瘤患者，一个肿瘤县、癌症县。我！我的家庭，东明县人民对北京充满了恐惧和悲哀。我心在哭泣，泪在眼里打转，视线模糊。我现在已经是第四次去北京 了，妈妈的甲状腺肿瘤做了3次手术，又扩散了，今年已经是70多岁的老人了，上了手术台能上下来吗？我弟弟做过两次手术又扩散了，弟媳做过一次手术，今天 又扩散了，他们人到中年，正是事业有成的时候呀！妹妹是刚结婚不久，原来很庆幸自己没有甲状腺肿瘤，可在前几天单位体检时也发现自己长了甲状腺肿瘤，他们 幸福的家庭还没来得及要孩子呀！我呢？我也是甲状腺肿瘤转移。天哪！离开东明――这片生我养我的热土，曾经无污染，碧水、蓝天、充满生机的小城镇。这，也 许是永别！心里充满了茫然、绝望。别了，爹、娘、哥、姐，东明的父老乡亲，让我再看您们一眼，让我再多看你们一眼，看一眼这里熟悉的一切，这里的床、写字 台、用惯了的钢笔、丈夫刚买了的粉红色的纱巾，这里的房子、这里的左邻右舍。我在房间里徘徊着，来回盲目的走动着。最后让我再看一眼熟睡的孩子，亲吻一下 稚嫩的脸蛋，天真可爱的脸庞在睡梦中露出甜甜的笑，难道他长大也要遭此厄运吗？也要在脖子上开几道的口子吗？肚子上也要开几道口子吗？孩子呀！你们不知 道，也许睡梦中醒来后，你已经没有了妈妈、没有了奶奶。妈妈呀，假如你下不了手术台，这，将是永别，再看一眼您的孙子吧！也许是最后一眼！您的孙子、孙女 会记住您语重心长的嘱托和朴素的语言，“孩子呀，好好学习考走吧，咱家的空气难闻，水也难喝。要是我不在了，在远方的十字路口画个圈，烧把纸就行了，奶奶 不怪你们….”苍天哪！我跪求您，把所有的肿瘤都给我一个人吧！假如，我的死能挽回全家人的平安幸福，能换回东明人民的健康快乐，我愿意立即跳楼自尽；假 如，我的死能唤醒治理环境，减少化工污染，遏制东明县肿瘤的爆发，还给东明人民碧水、蓝天，我愿意立即触电而死。如果不及时治理，只是追求政绩、经济效 益，生命没有了，健康没有了，人生还有什么意义？有什么意义呀！有什么意义呀！东明有多少个这样幸福的家庭将要分崩离析，要妻离子散呢？醒醒吧！少抽2包 中华烟，少喝2瓶茅台、五粮液，关注一下民生吧！关注一下东明县的环境污染吧！<br />
<br />
　　&nbsp; &nbsp; <br />
<br />
　　&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;收拾好了去北京用的东西，我檫了一把脸上的泪水，强装高兴 地说“妈，咱们走吧，去北京看看去，回来就好了。”，我故意骗我妈妈，说了这样一句善良的谎言，害怕她精神上支撑不了。就这样，我们一家五口人踏上了去北 京的列车。东明的空气仍然沉闷、凝重、难闻，我要窒息了。<br />
<br />
　　&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;别了，东明――曾经美丽的家乡，碧水、蓝天、鲜花、芳草。<br />
<br />
　　&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;我浑身酸软，双腿颤抖，心在滴血。。。。。。</div>

							
							
							
							
