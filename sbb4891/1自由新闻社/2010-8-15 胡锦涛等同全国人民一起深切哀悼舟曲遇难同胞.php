“人民在哭，你却在笑！”昨日是国务院规定的全国为甘肃舟曲泥石流遇难者哀悼日，山河溢悲情，但《人民日报》等三份中央喉舌报章，昨日头版竟是一张喜气洋洋彩色大相，相中主角中共总书记胡锦涛笑逐颜开，与国殇气氛形成强烈对比，引起读者愤怒，怒斥“这也叫国殇日”。学者不排除事件与权力斗争有关。<br />
<br />
惹起众怒的三份报章分别为中共中央机关报《人民日报》丶中央军委机关报《解放军报》及中宣部直接领导的《光明日报》，三份报章均为中共的喉舌报。据国务院规定，昨日是举国哀悼日，各地下半旗志哀，停止一切娱乐活动。各地媒体配合气氛，报纸和互联网头版均变黑色，电视除新闻和哀乐再无其他娱乐和笑容。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;<img src="https://yopic.us/images/91.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
《人民日报》昨日在头版刊登大相，胡锦涛笑逐颜开，与哀悼日形成强烈反差。<br />
<br />
但与全国一片悲情相反，《人民日报》丶《解放军报》丶《光明日报》等报章的报头虽改用黑色，头版所用大相却令人不可思议：相片是中共总书记胡锦涛接见出席全国援外会议的代表，不但相片鲜艳，且相中充溢喜气洋洋气氛，其中主角胡锦涛更是神采飞扬，笑到眉开眼开，全然没有一丝哀伤之感，与黑色报头丶与全国媒体沉重的表现，形成鲜明对比。记者昨在网上查看内地各大城市昨日出版的党报和主要报纸，中共北京市委机关报《北京日报》丶中共上海市委机关报《解放日报》等，头版如法照抄《人民日报》，其他报章均未看到以充满喜气的相片为大相。一些向以“不听话”着称的地方报，昨日相当凝重，以黑色志哀。其中，广州《南方日报》头条新闻并非胡锦涛，而是哀悼日，大相是两手托烛光黑白相，各子报亦一片悲情。<br />
<br />
中共三大喉舌报章头版昨日如此编排，引起读者和网民注意，其电子版被转贴到各社交网站和一些论坛，包括推特（ twitter）丶天涯论坛及凯迪论坛等，网民愤怒留言：“人民在哭，你却在笑！”“哀悼日，该被哀悼的人笑逐颜开！”“这也叫国殇日？跟庆祝国庆差不多了！”“涛哥（胡锦涛）！您慢慢笑吧，有您笑的！”“报纸总编应该向人民谢罪下台！”也有网民哀叹：“这就是中国政治，只许州官放火，不许百姓点灯！”<br />
