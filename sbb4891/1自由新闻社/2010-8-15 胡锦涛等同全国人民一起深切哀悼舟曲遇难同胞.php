这些报纸刊出的文章言论不但代表了中共思想理念，更可能是中共全党、全军遵循的方针政策。其中，上述三大喉舌报章每日的头条及大相，均由中共中央宣传部指定，现中宣部长为刘云山，而主管中宣部的中共政治局常委为李长春，他被视为前任总书记江泽民的人马。<br />
<br />
巨款撑媒体控舆论<br />
<br />
其中，《人民日报》为中共中央机关报，《解放军报》为中央军委机关报，《光明日报》初时虽由非中共的民主派创刊，号称代表知识界，但经数十年沧桑已被中共收编，成为中共中央机关报之一。<br />
<br />
与《人民日报》、《解放军报》及《光明日报》三份报章「乞人憎」有所区别是，昨日三报所属的网站「人民网」、「中国军网」及「光明网」首页则贴近主流，并没用「胡锦涛笑容」作主相，而是通版黑色处理，未知是自觉，抑或收到投诉而改过。<br />
<br />
改革开放后内地报纸、杂志及电视台遍地开花，但一言蔽之，所有媒体无不在中共一党统治之下，宣传报道内容多要仰中共首肯取舍；故代表中共中央的《人民日报》、《解放军报》及《光明日报》，依然拥有无可取代的龙头地位。<br />
<br />
近年，财大气粗的中共为抢夺舆论话事权，拨出巨款给上述喉舌媒体，要他们扩大阵营、新办媒体，以壮大中共「软实力」，例如《人民日报》由原来每日 16个版扩至 20个，驻国内国外 72个记者站全部升级，改为分社；新华社则办英文电视台，热闹一时。专家指，中共虽是铁幕政治，但党内斗争常可能透过喉舌媒体反映出来，例如一些重要场合，某位该出相片的领导突然消失，很可能意味着其在党内失势。<br />
<br />
暗示派系斗争端倪<br />
