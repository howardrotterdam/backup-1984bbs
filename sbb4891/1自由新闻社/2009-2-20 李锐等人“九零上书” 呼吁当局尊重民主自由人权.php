													<h2>李锐等人“九零上书” 呼吁当局尊重民主自由人权</h2>
						
						
																											<div id="postmessage_35966" class="t_msgfont">【《争鸣》杂志编者按】本刊收到《关于克服经济困难开创改革新局面的建议》文本，是李锐等老共产党员给胡锦涛并中共中央政治局常委的上书。还有一批老共产党员没有签名，认为上书精神有违&quot;两头真&quot;。本刊发表这份上书，意在引起评议、争论。在反民主、反普世价值的歪风冲击下，推动中国的思想前进是不可推卸的责任。<br />
<br />
关于克服经济困难开创改革新局面的建议<br />
<br />
锦涛同志并政治局各常委同志：<br />
在改革开放三十周年又值金融海啸袭来之际，我国正处于经济增速下降和社会矛盾上升的关口，我们这些八九十岁的老党员细读了锦涛同志在党的十一届三中全会纪念大会上的讲话，忠心地拥护锦涛同志所号召的&quot;坚定不移地坚持十一届三中全会开辟的道路，勇于变革，勇于创新，永不僵化，永不停滞，不畏任何风险所惧，不被任何干扰所惑，继续奋勇推进改革开放和社会主义现代化事业。&quot;研读了锦涛同志写给中国人权研究会的信和温家宝同志在中南海经济界人士座谈会上的讲话，我们认为，以人为本的口号深得人心将人权的普遍性原则与基本国情相结合，保证全体公民平等参与及平等发展的权利，如此落实以人为本，执政为民的口号，这个思路非常好。温家宝同志强调，越是在困难和复杂的情况下，越是要加强民主决策，加强决策的透明度，加强决策的民主监督。民主、透明、监督这六个字，体现了十七大提出的&quot;依法实行民主选举、民主决策、民主管理、民主监督，保障人民的知情权、参与权、表达权、监督权&quot;的决策，应该成为各级党委和各级政府的行为准则。上述思路，顺应潮流，合乎民意，我们坚决支持。具体如何操作，我们有如下建议：<br />
我们非常赞同中央投入四万亿人民币拉动经济同时十分担心特权和腐败分子乘机自肥，破坏党和人民的关系，激化社会矛盾，我们建议，今后凡关重大的四万亿投入的计划、项目均要严格履行真实有效的民主程序。党内首先要真正实行委员会票决制度，严惩个人独断拍板的第一把手，严禁巧立名目，借机搞政绩工程和滥建楼堂馆所。人大要拥有充分时间，真正审查四万亿元的财政收支。政协和各党派各社会团体应全程参与四万亿元开支的决策和使用。<br />
与四万亿元有关的重大决策的产生和实施全过程要透明、公开，向所有媒体开放，鼓励并责成媒体追踪报道。绝对禁止封杀和打压媒体的行为。1953年和1988年中央颁布的有关媒体批评报道的规定，应重申有效。其中，有关记者反映任何一级领导人的内参稿件，无须报送被报道本人及有关上级部门，可径直报送中央的规定，应特别加以重申。汶川地震时期，全国媒体全力以赴，公开、透明地报道了党和政府领导的伟大抗震救灾的全过程，向全世界展示了中国的不屈形象，赢得了国内外的广泛赞誉，中宣部在总结2008年的新闻报道工作时，提出了导向正确、及时准确、公开透明等五项经验和透明度决定公信度等三条体会。我们建议，将汶川抗震救灾的成功经验规范化制度化，施于长期。目前的经济困难尤甚于地震，必须保障媒体的宽松环境，保护媒体作全公开全透明的报道。这对威慑贪腐、凝聚人心，共度时艰，有着不可代替的巨大作用。<br />
增强监督机构的独立性。党的各级纪律检查委员会应真正实行上下垂直领导，不受同级党委干预，以保证公正办案。<br />
扩大民间各种社会组织发育的空间。汶川救灾。民间慈善组织发挥了政府难以替代的作用。重庆出租车停运的劳资纷争，政府居中调解，效果很好。将此类经验推而广之，保护各种民间组织自主发育，引导工人农民有组织地依法表达和追求自身利益，政府为各方提供协商平台，既有助于增加内需，又有利于缓解贫富矛盾和官民矛盾，还可以减少群体事件的不断发生，提供更高效廉洁地公共服务。这是长治久安之道。我们建议，认真总结重庆和慈善救灾事业的经验，逐步推广。<br />
恢复1986年成立的中央体制改革领导小组，在十三大报告的基础上，定期研讨，提出近一步改革的目标和方案。我们的经济体制从计划转向市场之后，政治体制改革严重滞后，权力进入市场，私用滥用，贪污腐败蔓延，党和人民蒙受了巨大损失，遏制腐败必须标本兼治，制约和监督权力的根本性制度极待建立。<br />
建议从中央做起改变作风，摒弃官话套话，减少&quot;重要指示&quot;&quot;重要讲话&quot;之类的话语。同时，建议公车使用、公费旅行、公费宴请等问题应有进一步改革方案。另外，许多国营企业，尤其金融、电力、电信等垄断企业的领导人，年薪高达几十万几百万甚至上千万，很是骇人听闻（当年一级干部月薪500元，与最低一级干部工资相差不足十倍。）建议主动减薪，以示与民共度难关。<br />
以人为本，执政为民，落实并保障宪法规定的公民权利，这个方向是完全正确的，考虑到中国国情，前进步子太猛，党和国家尚无此承受能力。所以必须在党和政府领导下，有章法地逐步有序推进。我们知道，就是这种小步推进，也面临着重重困难。<br />
锦涛同志和常委各同志：30年前，在经济频于崩溃之际，邓小平、胡耀邦等同志以大无畏气概推动中国改革开放的伟大事业，中国因 而走出危机。30年后，我国的综合国力增强，民生显著改善，国际威望空前提高，国人坚信我们能够度过难关，这样好的执政基础和政治局势，是30年前所没有的。粉碎四人帮后国家那么困难，但我们胜利了。现在人民站在你们一边，老干部站在你们一边，越是困难越要民主，越是困难越要透明，越是困难越要监督，我们党一定胜利！有道是：沧海横流，方显英雄本色。既然以人为本，执政为民，落实并保障宪法规定的公民权利的方向完全正确，就需要你们排除各种干扰，尤其是既得利益集团的干扰，行且不息，日有寸进，开创改革开放的新局面。<br />
此致<br />
敬礼 <br />
冯健、朱厚泽、李锐、李普、杜光、杜导正、吴象、吴明瑜、张思之、何方、钟佩璋、袁鹰、高尚全、彭迪、彦修、魏久明<br />
