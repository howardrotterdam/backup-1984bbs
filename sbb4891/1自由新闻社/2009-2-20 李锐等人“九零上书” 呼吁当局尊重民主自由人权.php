																											<div id="postmessage_54770" class="t_msgfont"><strong>八九十岁老党员请求民主</strong><br />
─ 评李锐等十六位老党员的「2009建议书」 <br />
（大陆）杨　麟 , 香港争鸣杂志3月号 <br />
<br />
　　二○○八年十一月，以三百零三位中年人为主体的爱国人士向社会发出公开信，提出《二○○八宪章》，真情呼吁民主宪政，但反而遭到不民主的打压，刘晓波丶张祖桦等人受到不民主的待遇。最近，二○○九年一月，由李锐丶李普等九十高龄的老党员领衔，十六位老干部签名提出了《关於克服经济困难，开创改革新局面的建议》（「二○○九建议书」），从体制内部按党章规定的合法程序向中共中央政治局各位常委上书，请求民主丶自由，呼吁开创改革新局面。据说，这份建议书，已经通过正式渠道送上去了。但是，天知道这种体制内的呼吁效果与後果如何，这些老人未来面临的是喜剧丶是悲剧，还是哑剧？如果只是聋子的对话，置之不理，扔之入篓，无动於衷，那倒无所谓，因为是意料之中丶情理之事。但愿这十六位忧党忧国忧民的八九十岁老人，不会遭到张祖桦丶刘晓波等三百零三人的政治待遇，不致於动员无产阶级专政机关进行拘审丶拘捕丶软禁丶抄家和所在社区丶单位党委的询问丶训导。但愿好人一生平安，而不是一生不平安。 <br />
　　<br />
&nbsp; &nbsp;&nbsp;&nbsp;细读十六位老人的「二○○九建议书」，有几点感想：<br />
<br />
　　<strong>一丶中国人没有「免於恐惧的自由」</strong><br />
<br />
　　这些从抗战时期就献身革命的八九十岁老党员向党中央提出的建议书，是站在体制之内，遵照合法程序丶采用官方话语，向政治局常委提出建议性意见，方式方法合乎党章与宪法。一反作者们平日行文尖锐丶泼辣丶鲜明的风格，<strong>建议书四平八稳丶滴水不漏，并且摸透政治行情，一点点都不碰「六三六五」和「批评揭发现领导及亲属」的政治底线，证明签名者具有相当高的政治智慧和自我保护的政治本能，同时也考虑了群众认识水平与可接受可传播的心理。</strong>当然，从「二○○九建议书」的形式与内容看，充分证明在中国的现实情况下，中国人没有「免於恐惧的自由」，包括这十六位具有相当功劳丶很大苦劳和疲劳的老党员在内，中国人充分享有着并将继续享有「恐惧的自由」。<br />
<br />
　　<strong> 二丶老人政治诉求的主要内容</strong><br />
<br />
　　──温家宝强调，越是困难和复杂的情况下，越是要加强民主决策，增强决策的透明度，加强决策的民主监督。「民主丶透明丶监督」这六个字，体现了党的十七大提出的「依法实行民主选举丶民主决策丶民主管理丶民主监督，保障人民的知情权。」参与权丶表达权丶监督权的决策，应该成为各级党委和政府行为准则。<br />
<br />
　　──中央投入四亿元拉动经济，我们十分担心特权和贪污分子乘机自肥，破坏党与人民的关系，激化社会矛盾。建议重大项目，严格履行真实有效的民主程序，党内真正实行委员会票决制度，人大真正审查四亿元的财政收支，政协丶各党派丶各社会团体全程参与四亿元开支的决策和使用。<br />
<br />
　　──汶川地震公开丶透明报道的成功经验规范化制度化，施於长期。记者反映任何一级领导人问题的内参稿件，可径直送中央。目前经济困难尤甚於地震，必需保障媒体宽松环境，保护媒体作全公开全透明的报道，绝对禁止封杀或打击媒体的行为。<br />
<br />
　　──党的各级纪委，真正实行上下垂直领导，不受同级党委干预。司法丶检查丶审计机构，依法行使职能，党委不得干预。<br />
<br />
　　──扩大民间各种社会组织发育的空间，，保护各种民间组织自主发育，引导工人农民有组织地表达和追求自身利益，政府为各方提供协商平台，有利於缓解贫富矛盾丶官民矛盾和减少群体事件。<br />
<br />
　　──恢复中央体制改革小组，提出进一步改革的目标和方案。政治体制改革严重滞後，权力进入市场，贪污蔓延，党和人民蒙受巨大损失。必须标本兼治，建立制约和监督权力的根本性制度。<br />
<br />
　　──从中央做起改变作风，摒弃官话套话，减少「重要指示」丶「重要讲话」之类话语。公车使用丶公费旅行丶公费请客等问题的进一步改革方案。金融丶电力垄断性国营企业领导人年薪几百万上千万，建议减薪，与民共渡难关。当年一级干部月薪五百元，与最低一级干部相差十倍。<br />
<br />
　　这些最低最基本的诉求，没有刺，花也不多，不知会结什麽果。但愿这些八九十岁的老人的陈情表，不致於遭到恐吓和训斥。如能有几条被接受丶采纳，则谢天谢地了。<br />
<br />
　　<strong> 三丶老干部希望现在的领导人「小步前进」丶「日有寸进」，「需要你们排除既得利益集团的干扰」</strong><br />
<br />
　　十六位八九十岁的老干部鼓励现在的领导人：「沧海横流方显英雄本色」。这些过去受过政治运动的深重迫害丶记忆犹新的老干部，二○○八年又看到写个「宣言」就被抄家丶软禁的张祖桦丶刘晓波的出头鸟丶折首鸡的现实命运，可能是为了保存自己的策略，<strong> 建议书中提出「现在人民站在你们一边，老干部站在你们一边，越是困难越要民主，越是困难越要透明，越是困难越要监督，我们党一定胜利！」他们希望「你们排除各种干扰，尤其是既得利益集团的干扰，落实并保证宪法规定的公民权利。」</strong><br />
<br />
　　十六位老干部忧心忡忡，「前进步子太猛，党和国家尚无此承受能力」，「必须在党和政府领导下，有章法地逐步有序推进」。而且「我们知道，就是这种小步前进，也面临着种种困难。」但愿老干部平安无事，这样低调地上摺子，如果还遭到打击，那末，谁还敢讲话？听不到不同声音的死寂社会，那就是衰亡的徵兆。至於老干部的品格，朱厚泽讲得好：两头真，後一头的真，也是很不容易的，要经得住实践的检验。我们每一个人都是历史的见证人，对所有的历史的见证人，历史也在见证着每一个人。我们相信，这些签名和未签名的同反民主的反动势力斗争了一生的老革命者，过去丶现在为民主丶自由丶人权而斗争，将来也必然站在真理一边，站在困难的中国人一边，站在浩浩荡荡的民主潮流之中。<br />
<br />
二○○九年二月十三日<br />
