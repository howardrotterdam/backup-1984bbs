<strong> 附文：　<br />
<br />
《零九上书》与《零八宪章》 </strong><br />
<br />
（大陆）钱非 香港争鸣杂志3月号<br />
<br />
　　近日，中共党内着名的民主派老人李锐丶李普丶朱厚泽丶杜导正等十六人给中南海上书，题为《关於克服经济困难 开创改革新局面的建议》。北京圈内人士将其称作《零九上书》，这显然是与《零八宪章》相对应的。从时间顺序和上书内容看，两者也是有着相当密切的关系。 <br />
<br />
　　首先，两者的内容有许多相似甚至重合的地方，但如果以温和理性的柔性化诉求作为一个指标来看，《零九上书》似乎与《零八宪章》在比赛，比谁更加温和丶更加理性丶更加柔性。这几乎把中国文化中<strong>「跪着造反」</strong>的政治传统发挥得淋漓尽致，也是中共体制内<strong>「跪着请愿」</strong>的所谓温和理性发挥到了极致。<br />
<br />
　　<strong>但是《零九上书》回避了《零八宪章》的宪政民主的政治改革诉求，其中更大的一个硬伤是：联署人竟然对中南海当权者说：「现在人民站在你们一边，老干部站在你们一边」。这显然是罔顾目前官民对立丶社会矛盾尖锐的严酷现实，也有违这些民主派长期以来自己对中共所作的批评，简直像在打自己的耳光。</strong> 正因为如此，这份上书在徵集签名的时候，遭到了一些人旗帜鲜明的拒绝，比如原《人民日报》社长胡绩伟等始终拒绝在《零九上书》上签字。也有一些评论认为，这个上书有损签名者的形象，也有违他们向来引以为荣的「两头真」本色。<br />
<br />
　　在《零九上书》中签名的人，除了原新华社副社长李普，其他人曾以各种理由婉拒在《零八宪章》上签名。虽然外界愿意相信，他们在理念上基本认同《零八宪章》，但是长期的政治经验告诉他们，死要面子的中南海当权者在现阶段是无法接受《零八宪章》这种签名运动的，尤其是这种运动是以相当规模出现的时候。结果也的确不出他们所料，胡锦涛对《零八宪章》采取了密不透风的压制，有关方面并且一再要求已经签名的一些党内外知名人士「退出签名」。<br />
<br />
　　不难看出，为避免《零八宪章》的「不足」，《零九上书》签署者们，<strong>首先要让中南海听得入耳，</strong> 才有可能让中南海接受他们的主张，为此该上书三易其稿，前两稿（包括李锐执笔的第二稿）都被否决，最後采用的是前新闻总署署长丶现任《炎黄春秋》杂志社社长杜导正起草的文本。<strong>上书因为要给中南海当权者带高帽子，不惜做一些妥协，作为一种政治策略，当然也是可以理解的，问题是当这种策略到了混淆基本事实的时候，显然就可能变成原则问题了。</strong><br />
<br />
　　当然，中南海如果能够接受《零九上书》，也不失为一种值得肯定的进步，也不失为上书者们的一番苦心。但是以「断然拒绝」《零八宪章》的中南海当权者的状况来看，接受《零九上书》的可能性也是完全不存在的，不惜一切代价维护既得利益的中共当权者，既无这种理性，也没有这种能力。在中南海眼里，《零九上书》是中共党内对《零八宪章》的响应，这些人比那些《零八宪章》的签署者还要坏。结果很可能是上书者们的幻想再次破灭，他们在用自己的政治声誉「飞蛾扑火」，真是「春蚕到死丝方尽」。<br />
<br />
　　尽管也有人将此视作民主派的内讧，《零九上书》似乎在为中南海处理《零八宪章》的窘困解围，客观上有可能分解《零八宪章》造成的促进中南海政治改革的民间压力。但是，历史的进步是合力的作用，中国社会发展目前一只脚已经跨进了多元化的门槛，《零九上书》展示了一段人们不应忽视的光谱，它与《零八宪章》还有其他丰富多彩的各种诉求，共同谱写了百花齐放的前奏曲。<br />
