													<h2>760亿地震捐赠80%流入政府财政专户 政府回应“NGO不可信”</h2>
						
						
																											<div id="postmessage_199867" class="t_msgfont"><a href="http://news.163.com/09/0812/08/5GGKJ4RV0001124J.html" target="_blank">http://news.163.com/09/0812/08/5GGKJ4RV0001124J.html</a><br />
<br />
中国青年报8月12日报道 <br />
<br />
南京一个名叫徐超的乞丐，把自己讨来的零钱兑换成百元大钞，塞进街头的募捐箱;上海一个外企白领从自己的工资卡上汇出1000元;北京一家报纸的总编辑取出10000元捐作特殊党费……<br />
<br />
去年“5・12”汶川地震之后，类似这样来自全国各地、各式各样的抗震救灾捐赠款物，截至今年4月30日，总数达到了767.12亿元(其中捐赠资金约653亿元，物资折合约114亿元)，被公认创下“中国捐赠史的新纪录”。<br />
<br />
在这个庞大的数目一天天累积的过程中，有些疑问被反复提及：这些钱物流向了哪里?<br />
<br />
最近，清华大学公共管理学院的一支团队对这些问题进行了研究。根据他们为期半年的调研，这些来自公众个人或企业腰包的钱，极可能80%左右流入了政府的财政专户，变成了政府的“额外税收”，由政府部门统筹用于灾区。<br />
<br />
这个数据，在这支团队的负责人邓国胜看来，很值得玩味。在不少西方国家，救灾时政府一般不接受民间捐赠，即使接受了，也会将钱交由民间组织去花。但在中国，事情显然不是这样。<br />
<br />
在80%这个数字背后，这位清华大学公共管理学院NGO研究所的副教授看到的，是一种不容乐观的现状。这是一个隐忧，虽然未必刺激公众神经，却关乎这个国家正在发生或正要发生的一场深刻的社会变革。<br />
<br />
被政府垄断的民间资源<br />
<br />
许多人愿意用“井喷”这个词，来形容去年那场大地震激发出的公众捐赠热潮。有数据显示，仅仅去年5~6月，在上海、北京和重庆这些城市的市民中，每10个人里就有9个为抗震救灾捐赠了款物。<br />
<br />
这些钱也许是通过单位的工会捐了出去，或许是塞进了某个公益机构在路边设立的一只不起眼的捐款箱，或许是通过党组织的特殊党费交到了中央组织部，又或许是通过银行或者邮局汇进了某个公益组织的募捐账户……<br />
<br />
这些钱在全国无数个账户之间流动，最后，有一半以上直接进入了政府的账户内。这其中包含了全国数千万党员捐赠的特殊党费、各地省级人民政府直接接受的捐赠、以及民政部设立的抗震救灾专户。<br />
<br />
准确地说，在邓国胜及其同事的调研中，截至去年11月，全国捐赠的资金为652.5亿元，其中政府直接受捐约占58%，约379亿元。这笔钱，毫无悬念地，由政府部门来使用。<br />
<br />
如果说这是“蛋糕”最大的一块，那么第二大块，则是流向各地红十字会、慈善会以及地方公募基金会的捐款。这一部分占了约31%，约199亿元。<br />
<br />
尽管根据国务院下达的文件，这笔钱原本可以由这些地方性公益组织自行安排使用，但是邓国胜团队在对全国7个省(市)进行的抽样调查中发现，这些捐款中的大多数，最后仍然交给政府部门去使用了。<br />
<br />
这一比例究竟有多高，邓国胜的团队没有给出确切的数字。但在调研中，他们拿到的事实是：在这些省份，这些地方性公益组织募集到的捐款，除去不多的必须按照捐赠者意愿进行使用的定向资金外，非定向资金大多转入当地政府的财政专户。<br />
<br />
事实是：有些省份，非定向资金必须要求转入政府财政账户。有些省份，这些组织可以对受捐资金留有一些使用权，不用全部转入政府的财政专户，但通常需要和地方政府一起到灾区开展援建项目。还有些省份，即使不用转入政府财政专户，但也仍然由政府统筹使用，然后从这些民间组织报账。<br />
<br />
事实是：有些省份，就连这些公益组织募集来的定向资金，也都要强行转入政府财政账户，由政府按照捐赠人的意愿来使用。<br />
<br />
事实是：在少数承担了灾区援建任务的省份，当地政府用于对口援建的资金中，竟然一半以上来自社会捐款，只有不到一半来自政府财政。还有些省份，这一比例甚至更高。<br />
<br />
现在，这块全国救灾捐赠资金的大“蛋糕”只剩最小的一块，流向了中国红十字总会、中华慈善总会和16家全国性公募基金会。流到它们盘子里的，只占整个救灾捐赠资金的约11%。在邓国胜团队绘制的表格里，只有这一块资金后面，注明的是“自行安排使用”。<br />
<br />
但这是怎样的“自行安排使用”呢?<br />
<br />
中国红十字总会、中华慈善总会这两家以往具有救灾募款垄断地位的“官办民间组织”，总共募集了约63亿元捐款。通常的方式是，两家机构将募集到的资金层层下拨到地方红十字会和地方慈善会。基层红十字会和慈善会往往执行能力弱，在有些地方，甚至只是县卫生局或民政局下属的一个科室。无论是为灾民建房、盖学校，还是盖医院，通常，资金最终还是流向了地方政府，地方政府成为项目的实际执行者。<br />
<br />
这是一条逆向的资金流动。难怪“希望工程”创始人徐永光会感叹说，国际上通行的是政府购买NGO(非政府组织)的服务，比如美国政府购买NGO服务的款额是民间捐赠总额的1.5倍，但中国却正好相反，变成了NGO“购买”政府的服务。“中国公募基金会向社会募捐后，都是和政府合作做项目，在项目落实的时候，干活的都是公务员。”<br />
<br />
尽管没有更确切的数字，但据邓国胜粗略估算，全国来自社会和民间的抗震救灾捐赠，最后流向政府、由政府来使用的，极可能在80%以上。至少，在他的团队调查的几个省份中，这个比例很高。<br />
<br />
没钱花和愁花钱<br />
<br />
某种程度上，邓国胜对此表示了理解。因为红十字会和慈善会这些组织让地方政府执行项目，便可以将成本转嫁给地方政府，同时也将风险转嫁给地方政府，对它们而言，“这不失为一个省心省力的好办法”。<br />
<br />
然而，“这不是方向。”这位学者摇着头说。<br />
<br />
在他看来，方向应当是“小政府，大社会”，是政府从垄断走向适度开放。为什么不把这些资金拿出来，委托外包给民间组织，从而激发社会的活力呢?邓国胜反问道。<br />
<br />
他相信，在公共服务的提供方面，政府有着自身的局限，“所以我们才要改革，在经济领域引入市场机制，在社会领域引入民间组织。”<br />
<br />
“一个社会有一群能够自我管理自我运作的民间组织，这是公民社会的重要内容。”邓国胜说。<br />
<br />
他认准的大方向是，这个国家在实现“经济开放”之后，如今到了该走向“社会开放”的时候了。<br />
<br />
“5・12”地震之后出现的景象曾一度令他感到“很激动”。据不完全统计，奔赴四川一线参与救灾的民间组织有300多家，介入的志愿者更达到300万人左右。许多人认为，中国的志愿者及民间组织以前所未有的态势登场，是这个国家“民间力量的第一次集体亮相”。<br />
<br />
乐观者相信，“2008年是中国公民社会元年”。更有研究机构高调宣布，中国已经进入公民社会。<br />
<br />
不过邓国胜审慎地表示并不认同，因为事情似乎还不那么令人乐观。<br />
<br />
自去年8~9月份始，曾活跃在灾区的志愿者和民间组织“潮水般”地退却了。在调研中，邓国胜的团队了解到的数据是，截至今年4月，坚守在灾区的民间组织估计已经不足50家，志愿者不到5万人。<br />
<br />
“缺乏资源和资金的支持，难以为继，这是非常重要的一个原因。”邓国胜说。<br />
<br />
这场地震，“就像是放大镜”，将长久以来存在的弊病集中显现。一直以来，国内草根NGO几乎无法获取本土资源的支持，它们的资金大多来自国外。地震之后，看到民间爆发出惊人的捐款热情，从事NGO事业近20年的徐永光原本以为，“这一次中国本土NGO一定能获取本土资源支持”，但事后发现，情况并没有明显改观。<br />
<br />
来自民间的捐赠最终大多数流向了政府，流向本土NGO的，少之又少。一个项目能获得三五百万元的资助，已经“非常可观”;能获得一二十万元资金，也“已经不错了”。<br />
<br />
所以也就不难理解，人们会听到这样两种截然不同的忧虑：<br />
<br />
一个名叫刘猛的草根NGO负责人忧虑自己快没钱了。他用自己的数十万元积蓄维持一个团队的开支，一度陷入困境，几乎吃了上顿没下顿，以至于他的妻子不得不从国外赶回来给他送钱。这个名叫“全国心理援助联盟”的组织，在都江堰市的板房区内为灾民做心理援助，最多时有200多个志愿者，最后只剩下少数人苦苦支撑。<br />
<br />
而民政部社会福利与慈善事业促进司的一位官员则在灾后忧虑说，现在几百亿元的捐款是悬在中国政府头上的“堰塞湖”。甚至有的机构，平均一个工作人员“摊到”好几个亿的捐款，该怎么花?<br />
<br />
无法确切知道，地震灾区究竟有多少草根NGO像刘猛那样靠自筹经费、自掏腰包在维持。据在灾区调研的学者们说，“不在少数”。甚至，四川省社会科学院研究员郭虹认为，志愿者和NGO大量从灾区撤走的最主要原因，就是缺乏资金支持。<br />
<br />
“中国公民社会的道路还很漫长”<br />
<br />
80%以上的社会捐赠资金最终流向政府，这样的事实意味着什么，看起来公众对此并不像邓国胜那样感到焦虑。<br />
<br />
今年5月，邓国胜团队与一家市场监测机构联合进行了抽样调查，结果发现，在2000多个有效样本中，60.8%的受访者认为，救灾时，社会捐赠资金应该由政府统筹使用，只有9.7%的人认为不应该，还有29.1%人表示无所谓。<br />
<br />
学者们承认，“弱小，是中国民间组织共同的特征”。美国一家名叫联合之路的慈善机构2007年一年就能筹到40亿美元，而中国超过1300家基金会同一年只募到约6亿美元。<br />
<br />
学者们也承认，中国的民间组织执行能力常常还不够强。他们还承认，这些民间组织大多在财务公开、信息透明方面做得不够正规和完善。而间或曝出的丑闻也令它们的公信力大打折扣。<br />
<br />
但在邓国胜看来，“不能因为目前没有能力，就不给你机会。有时候虽然政府能比它们做得更好，但这不是方向。”<br />
<br />
否则，政府的大包大揽，将让这些民间组织的发展陷入恶性循环。“因为你弱，就不给你机会;不给你机会，你只会更弱。”邓国胜说。<br />
<br />
邓国胜团队的调研成果，将于8月12日在北京开幕的“社会组织5・12行动论坛暨公益项目交流展示会”上发布。与这一成果一起亮相的，是其他数位学者围绕民间组织参与救灾展开的多项调查。<br />
<br />
在中国民间力量“集体亮相”一年多之后，由中国扶贫基金会、中国红十字基金会等21家基金会和NGO联合发起的这一大型论坛，将对中国NGO参与地震救灾的行动和机制进行总结、反思与展望。<br />
<br />
邓国胜已经不再像一年多前那般乐观。坐在办公室里，这位瘦瘦的学者感叹说：“中国公民社会的道路还很漫长。” <br />
<br />
(本文来源：中国青年报 作者：包丽敏)</div>

							
							
							
							
