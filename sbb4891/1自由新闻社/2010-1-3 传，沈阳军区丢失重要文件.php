<br />
　　刘连昆同时在两岸拥有少将身分达七年之久，是台湾国防部军事情报局改制以来最重要的策反工作，代号「少康专案」。国安局长丁渝洲曾形容少康专案是军情局的「镇山之宝」。刘连昆从一九九二年正式加入台湾军情局工作，至一九九九年被逮捕、处死，七年期间提供无数重要情报给台湾，被认为是我国建国五十年来最严重的间谍案。<br />
<br />
