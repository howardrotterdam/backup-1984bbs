　　紧张的时刻来临，如何确定这场高层谍报会不是陷阱。台湾方面先由「越界交通」与刘共进早餐，探个口风。王姓少将则单独前往双方预定会面的广州越秀公园，沿路则在新华书店等地点暂停，观察有无被大陆安全人员跟监。<br />
<br />
　　进入这座位于广州郊区的公园后，双方人马保持可见的距离，然后利用无人的羊肠小径，爬上隐密的山岗，于大石上席地而坐，展开两岸情报圈首次层级最高的「目视接触」。刘开始有点紧张，会后双方分别前往广州「东方轩」酒店聚餐时，气氛才转为轻松。刘连昆当场还点了一瓶茅台酒，双方闲话家常，完全看不出是一场惊心动魄的间谍会。<br />
<br />
　　在这场间谍会中，台湾告知了刘的待遇、联络方式、重点工作。待遇比照国军少将编阶，月薪是每月叁千五百美金(以九二年币值计约新台币九万元)，工作奖金另发，每批情报少则台币四十万元，多则百万元以上，并代为转存于海外银行专户。退休后由军情局照顾生活及福利，并依个人意愿安排至国外或台湾居住。为表示对这次会面的重视，台湾方面拿出两万美金红包，做为见面礼。<br />
<br />
　　双方闲聊过程中，刘连昆透露是东北齐齐哈尔人，一九叁叁年生，后勤学院毕业，长期负责军械采购和产制，当时担任军械部长已六年，常出席各军总部重要工作会议，接触各种极机密资料。军中人脉甚广，与中共将领洪学智、赵南起、刘华清均熟识。<br />
<br />
　　当天，刘连昆并透露，64时，他曾发表同情学生的言论，因而遭到上级警告。而38军军长被拘禁时，他还曾托人请代为营救。不过真正令其不满的是，他希望升中将后退休，但军方未予理会。刘连昆在酒后透露，其任内因采购军品，得罪了彭真的子女，同时，军委会副主席张震也刻意打压他。<br />
<br />
　　刘连昆于是成为两岸第一位同时拥有少将身分的间谍。<br />
<br />
　　「台湾的刘少将」参加情报工作后，态度积极，表现优异。例如，我国新购SU-27战机后，刘立即提供了驻地湛江、芜湖，导弹放置的杭州空军军械库、及洛阳维修厂等地详情，另外，他也提供过我军应急机动作战部队番号、驻地及军演状况。<br />
<br />
　　九四年，军情局再度派出代表密晤刘连昆，主要任务是希望再上一层楼，建立更高阶的网络，收集我国对台作战纲要及预警情报，并向刘探寻是否有合适接班人选。<br />
<br />
　　但是，这次台湾代表从澳门拱北关入境时被广东省国安厅的人盯上，虽然利用了广州警备区的BMW豪华大轿车代步掩护，但从下榻的「华厦大酒店」一路被跟踪，安全部门两人一组利用机动性高的摩托车对付BMW。台湾代表从原先会面的地点「白天鹅宾馆」佯装先开往「东方宾馆」，但仍无法摆脱，只好冒险硬闯「白天鹅」，幸好安全厅人员因人手不足，未立即跟进旅馆，台湾代表终于在房间中见到刘连昆，双方短暂交换意见立即分手。<br />
<br />
<br />
　　当晚台湾代表不经意放出次日将下乡游览的讯息，但隔天一大早却直奔广州车站，摆脱盯哨，快速离开大陆。<br />
<br />
　　整个过程宛如００七电影，为何没有马上逮捕台湾代表，主要是想抓大鱼，知道大陆主角是何许人，所幸刘连昆并未被发现。事后台湾秘密调查，发现出卖这次行动，竟然是香港站的专员，随后诱捕回台判处无期徒刑。<br />
<br />
　　这次行动，刘连昆提供了二十件重要情报，并包括一份可试图策反的中共将领名单，刘连昆表示，如果没有中共的盯哨，甚至可安排台湾代表到北京会见一位更高层的将领。<br />
<br />
　　根据北京消息，刘连昆和邵正宗的情报网络确实在暗中吸纳解放军的高层将领及情报。四大部长之一的总装备部部长曹刚川，在任副参谋总长期间，邵正宗就奉刘连昆指示，向曹刚川周边人士进行策反。曹刚川与刘连昆均先后在总后勤部军械部任职。刘连昆后来曝光，虽然过去有人说是肇因于前总统李登辉的「哑弹论」、「十八套剧本说」。然而国安局了解内情人士透露，其实和军情局内部人事倾轧、争功，过度利用刘连昆的关系更大。在他为台湾工作的七年间，许多台湾军情局官员，包括前局长殷宗文等，因着刘连昆提供的珍贵情报，获得高额奖金、升官，甚至高层宠信。<br />
<br />
　　为了争取绩效，舍不得放弃「大鱼」，是情报工作的兵家大忌。台湾不断利用高额工作奖金，引诱刘连昆继续冒险。更令人痛心的是，还有国安官员鼓励刘提供中共原始文件，因为和影本比起来，真本可以让经办官员和刘连昆同获五倍以上的奖金。由于人人想分这杯「肥羹」，变得多人经手刘案，「线民」身分终于不保。<br />
<br />
　　情报局虽在刘被捕后，成立「运通专案」检讨，并撤换至少一名少将级主管，但仍难弥补巨大损失。因为情报单位的争功诿过，损失的还不止「少康案」，过去两年，还有大陆多名重要干部被捕。<br />
<br />
　　巧合的是，台海双方在检讨刘案时，均将其视为六十年代初，苏联着名间谍「潘考夫斯基」的翻版。潘考夫斯基在古巴导弹危机时，提供苏联弹道导弹发展关键情报给美国，协助美国政府和平解决危机，但却因为西方情报单位尔后为了绩效、过度利用这条线，同样导致他曝光、最后被枪决。<br />
<br />
　　刘连昆生前为台湾工作，是否有多么崇高的理想，现在不得而知。但他曾对一位(台湾)军情局代表说过：「你是我的知交」。爆出一九九六年台海危机期间，大陆解放军内部强硬派曾在秘密会议中，真的主张要用导弹攻台，甚至要在台几个小区域用小型核弹轰击。<br />
<br />
　　可是由於李登辉总统为了自我炫耀台掌握了大陆九六年在台海演习发射的导弹是空弹，使台在大陆布建的情报网曝光，稍後更导致台策反大陆两名解放军将领被处死，李登辉在东窗事发後，对谍报人员更采取过桥抽板的态度。<br />
<br />
　　事缘九六年台海危机中，「大陆演习导弹所发是空弹」的消息，是透过刘、邵与张志鹏的管道带回台湾。不料李登辉政府轻易的说出了这项机密情报，由於参与大陆解放军演习会议的将领，不足十人，台湾竟能知道「空弹」的精确消息，触发大陆大规模搜查台谍，刘、邵二人被抓，并於八月十五日被处决，此案株连共廿四人。<br />
<br />
　　张说，他曾因李登辉两次提出「明统暗独」的言论，向当时任情报局长的殷宗文表示，不愿为这样的国民党力，但殷向他拍胸膛保证，国民党绝不搞台毒。<br />
<br />
　　东窗事发台无意援救在东窗事发後，张仓皇逃离大陆，在台湾也差点流落街头，至今只收到军情局十五万新台币，殷也称病一再拒绝接见他；邵的沈姓女友亡命欧洲；台商姚嘉珍被大陆判刑，台政府却无诚意援救。<br />
<br />
　　台湾国防部及所属军情局表示，由於台在大陆情报工作，涉及机密，为保障情报管道安全，不对情报问题作任何评论。<br />
<br />
　　在1996年台海危机解放军向台湾试射导弹期间，由于李登辉一句共军打的都是空包弹，天一下雨就点不着了，北京当局察觉解放军高层内部有间谍，并於去年叁月侦破台湾间谍网，处死为台湾提供情报的解放军少将刘连昆和大校邵正忠。如今，此案中其他为台湾做间谍的人如今有的逃亡国外，有的被关押，而台湾政?人员生死听天由命，令仍然活跃在情报工作第一线的台湾间谍们心凉胆寒。<br />
