<br />
　　台湾方面成立的「少康专案」，则以争取「少康二号」刘连昆为首要目标，并决定由前军情局局长殷宗文亲自主控这条「大线」。九二年十一月，台湾方面派出军情局第六处王姓上校副处长，加升一级，以少将身分前往大陆，亲自争取刘连昆为台湾工作。<br />
<br />
