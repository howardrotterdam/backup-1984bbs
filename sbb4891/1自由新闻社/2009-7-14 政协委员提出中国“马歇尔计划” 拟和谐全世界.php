																											<div id="postmessage_161600" class="t_msgfont">政协委员献计出口 提出中国版“马歇尔计划”<br />
2009-07-13 　来源: 中国新闻网(北京)　<br />
许善达建议，中国用五千亿美元的规模建立“和谐世界计划”或“共享发展计划”的基金，向亚洲、非洲、拉丁美洲等发展中国家宣布中国愿意向友好国家提供国家贷款用于借款国的基础设施建设。<br />
<br />
中新社北京七月十三日电 为应对当前出口颓势，全国政协委员、国家税务总局原副局长许善达十三日在此间会议上提出中国版“马歇尔计划”，即“和谐世界计划”或“共享发展计划”。<br />
<br />
此间全国政协专题协商会上，许善达提交发言指出，由于金融危机的影响，目前国际市场形势已经出现了“实质性”变化，“优质低价”的出口战略已经不可能实现中国的目标了。但是，过剩的相当数量的基础设施建设的产能、大量的外汇储备和极高的人民币信用是中国拥有的两项重要的资源优势。<br />
<br />
“中国现在有相当多的发展中国家拥有待开发的资源但没有资金开发，同时这些国家拥有对基础设施的强烈需求也同样没有资金建设。”许善达认为，中国的资源和这些国家的资源如果组合起来可以使彼此经济同时发展并共享发展的收益。<br />
<br />
许善达所提“和谐世界计划”或“共享发展计划”几乎是美国“马歇尔计划”的翻版。他说，“马歇尔计划”在“二战”以后不仅促使欧洲复兴，也让美国从中大大地受益。从经济角度考虑，我们今天面临的形势与“二战”后美国相似，即产能大量过剩。<br />
<br />
据介绍，“马歇尔计划”就是美国拿出其黄金储备的三分之二(约一百三十亿美元)借给受援国，让他们购买美国的东西。即把美国过剩的产能转化成美国政府对受援国家的债权。美国再动用各种力量，确保这些借钱的国家不敢赖账。与此同时，在美国主导下又确立了布雷顿森林体系，规定三十五美元兑换一盎司黄金，受援国如没有美元，要用黄金来还债。产能输出加上国家债权再加上布雷顿森林体系，使美国成为“马歇尔计划”的最大受益国。<br />
<br />
许善达建议，中国用五千亿美元的规模建立“和谐世界计划”或“共享发展计划”的基金(其中：一千亿美元或其他外汇规模，三万亿人民币规模)，向亚洲、非洲、拉丁美洲等发展中国家宣布中国愿意向友好国家提供国家贷款用于借款国的基础设施建设。渠道可借助现有的中非合作论坛、上海合作组织、东盟自由贸易区等等。<br />
<br />
同时，该贷款主要是商业性的，但利息比一般商业贷款低。中国政府只要求获得扣除风险之后的利息高于美国国债。少量贷款可以是无偿的。借款国政府是借款人，自愿申请项目贷款并履行还本付息义务。项目建设和采购要在中国企业中进行。<br />
<br />
许善达特别说明，该计划总计五千亿美元的基金规模不大，有助加快人民币国际化步伐，有助减少外汇储备、规避中长期美元贬值风险。计划的核心内容是：国家承担贷款风险，企业输出过剩产能，人民币国际化，三位一体。 (本文来源：中国新闻网 作者：徐长安)</div>

							
							
							
							
