[编辑]俄罗斯金融危机概述 <br />
　　俄罗斯自1992年开始全面的经济转轨以后，一直陷于严重的经济危机中。通货膨胀、投资萎缩、生产下降伴随&quot;休克疗法&quot;和私有化改革的推进而发展。受东亚金融危机的波及，俄罗斯于1997年10月和1998年5月先后爆发了两次金融危机。危机首先反映在货币市场和证券市场上，占俄罗斯国债总额约1/3的外国资本大规模外逃，引起汇率下跌和股市大幅下挫，再贷款利率一度高达150%，股市、债市和汇市基本陷入停盘状态，银行无力应付居民提款，整个金融体系和经济运行几乎瘫痪。1998年8月，政府决定让卢布自由浮动，宣布单方面延期偿付以卢布计价的内债和部分外债，并禁止银行兑现外汇承诺，金融危机全面爆发。? <br />
<br />
　　其特点是，金融大波动的间隔越来越短，规模越来越大，程度越来越深，最终导致两届政府的垮台，甚至波及全球，产生全球效应。 <br />
<br />
　　第一次金融风波发生在1997年10月28日至11月中旬之间。 <br />
<br />
　　本来，俄罗斯自1992年初推行“休克疗法”改革后到1996年生产连续下降，到1997年才出现止跌回升，但升幅很少，只0.8%[1]。俄于1996年起对外资开放，人们看好俄金融市场，纷纷投资股市和债市，因股价上升潜力大，回报率高。俄股票面值定得很低，平均只值50美分到4～5美元之间，股票回报率平均高达1倍以上；国债的回报率也在20%以上，而且80%是 3～4个月的短期国债，兑现快。1997年是俄经济转轨以来吸入外资最多的一年。俄从1991年起一共吸入外资237.5亿美元，其中1997年即达 100多亿美元。但是外资总额中直接投资只占30%左右，70%左右是短期资本投资，来得快，走得也快，这就埋下了隐患。1997年10月间，外资已掌握了60%～70%的股市交易量，30%～40%的国债交易额。 <br />
<br />
　　1997年7月泰国首先爆发的金融危机对俄金融市场的影响还不大，因8～9月间还有大量外资涌入。及至10月韩国大爆发金融危机立即对俄金融市场产生连锁反应。因在俄金融市场中韩资占有一定比重。韩本国发生金融危机，韩资急忙大量撤走，以救其本国之急，其他外国投资者也纷纷跟进，结果，自1997年10月28日到11月10日间由于大量抛售股票，股价平均下跌30%，股市殃及债市和汇市，后者也纷纷告急。当时央行拿出35亿美元拯救债市，以维持国债的收益率吸住外资。虽然国债收益率上升至45%，但外资依然撤走了100 亿美元。 <br />
<br />
　　第二次金融大波动发生在1998年5月～6月间。 <br />
<br />
　　这次大波动的诱因则主要是国内的“信任危机”引起的。这次至少抽走资金140亿美元。 <br />
<br />
　　具体原因主要是3条： <br />
<br />
　　1、长达月余的政府危机引起投资者对俄政局的不安。 <br />
<br />
　　1997年3月23日总统出于政治考虑，突然解散切尔诺梅尔金政府及解除其总理职务，引致政府、总统与杜马在新总理任命问题上的争斗。经过三次杜马表决才勉强通过基里延科总理的任命。在这一个月政府危机期间，经济领导受到很大影响，政府少收税款30亿美元，使拮据的财政更是雪上加霜。同时，由于新任命总理基里延科年轻，资历浅，缺乏财团、政党的支持和治国经验，人们对新政府信心不足，那时已有部分投资者开始撤离。 <br />
<br />
　　2、俄罗斯严重的财政、债务危机突然暴露在世人面前，引起投资者的心理恐慌。 <br />
<br />
　　其实，俄自1992年以来一直存在财政赤字，由于政府采取发行国债、举借外债、拖延支付等所谓“软赤字”办法加以弥补，再加上偿付债息不包括在预算支出内，因此公布的财政赤字不高(除 1994年赤字占GDP10.7%外，其余年份均在3%～4.6%之间)民众不甚了解其实际严重程度(实际在8%～10%之间). 1998年大笔债务陆续到期，拖欠需要偿还，新政府要承担偿债任务，责任重大，才公布了财政债务危机的严重情况。俄罗斯生产一直下降，财政收入基础越来越小；再加上税种过多，税率过高，引致企业税务过重，因此逃税现象十分普遍，几乎一半单位偷、漏税；拖欠工资额不断增加，1998年上半年又增200亿卢布，总数达700亿卢布。国际能源价格下降，使俄少收入50亿～70亿美元。俄为支撑经济生活运转，不得不大量借新债还旧债，而且要借更多新债。除还内债外，还要弥补财政缺口，于是债务越滚越多，形成债务金字塔。到基里延科接任总理时，俄内债达700亿美元(其中国债达4500亿卢布), 外债达1300亿美元。1998年预算中偿旧债和补赤字加在一起，占国家开支的58%。当时财长承认，今年至少需再借100～150亿美元才能渡过难关。 <br />
<br />
　　3、议会修改政府的私有化政策，是引起这次金融市场波动的导火线。 <br />
<br />
　　“俄罗斯统一电力系统股份公司”已有28%的股票售予外商。可俄国家杜马又专门通过关于该公司股票处置法，规定外资拥有该公司的股票份额不得超过25%。这样一来，引起外资对俄政府的不信任，纷纷抛售股票。这个公司的股票在两周内下跌40%，别的股票也跟着下跌25%～40%。受此影响，国债价格急剧下滑，收益率被迫由50%上升至80%，更加重了政府还债的负担。美元兑卢布的汇率上升到1∶6.2010～6.2030，超过俄央行规定的最高限额6.1850。而且，这次私有化政策变动已影响后续私有化的推行。最明显的例子是俄罗斯石油公司拟出售75%股份而无人问津。当然，这也与世界石油市场价格暴跌、人们不看好石油生产有关。 <br />
<br />
[编辑]俄罗斯金融危机的爆发原因 <br />
　　三次金融大风波的根本原因是由于长期推行货币主义政策，导致生产萎缩，经济虚弱，财政拮据，一直靠出卖资源、举借内外债支撑。但具体诱因，则有所不同。第一次大波动主要是外来的，由东亚金融危机波及之故，第二次则主要是俄政府的政策失误，引起对政府的不信任所致，国际金融炒家染指俄金融市场也是产生全球效应的一个重要原因。 <br />
<br />
　　具体来说，俄罗斯金融危机爆发的原因主要有： <br />
<br />
　　1、出口受阻和资本急剧外流导致国际收支恶化。 <br />
<br />
　　从1994年到1997年，俄罗斯每年都保持着巨额贸易顺差和适度经常项目顺差。但1997年的亚洲金融危机使得全球石油价格与原材料价格下跌，俄罗斯的出口贸易受到沉重打击（俄罗斯的外汇收入主要依靠石油和天然气出口。据统计，1998年俄罗斯石油外汇收入比1997年同期减少了约60亿美元，若再加上其他资源品出口的降价损失，实际外汇收入减少了100亿~120亿美元。）。另一方面，尽管俄罗斯有足够的资本流入（包括适中的国外直接投资、持续的IMF援助以及1996年至1997年间大量的证券投资流入。），但同时也存在巨额的资本流出（这是由两种截然不同的预期所造成的不正常现象。一方认为会继续繁荣，另一方则对此没有信心。），从而造成俄罗斯外汇储备增长缓慢（从1993年底到1997年底仅增加了72亿美元。）。1997年7月，资本流出急剧加速，进一步恶化了国际收支，直接引发了金融危机的爆发。 <br />
<br />
　　2、巨额财政赤字与债务负担。 <br />
<br />
　　俄罗斯自独立以来经济长期处于动荡和衰退的局面，导致财政入不敷出，到1998年财政缺口高达180亿~190亿美元（尽管财政赤字占GDP的比重有所下降，但主要是由于联邦政府支出降低，而联邦政府支出的降低并不是通过实施一套完整的削减政策形成的，而是简单地通过推迟发放工资、养老金，推迟支付商品和劳务供应商的款项来实现的。）。为解决国家财政的困窘，俄政府不得不大量发放短期债券和对外大量借款，从而导致严重的债务危机。 1997年底俄罗斯可以确认的对外负债总额为1280亿美元，资产总值为277亿美元，总资产中外汇储备为130亿美元，是对外负债总值的1/10。 <br />
<br />
　　3、俄罗斯在制度和结构方面的薄弱性。 <br />
<br />
　　这是俄罗斯在经济转轨过程中形成的深层矛盾，主要表现在政府不能制定和实施一个连贯的经济政策；公共管理如同虚设；制度框架要么不存在，要么无法实施；一些法律和制度基础几乎完全缺位等（例如明确的产权界定、清晰的税收体系以及破产法等方面。）（Alexandre Lamfalussy，1999）。这些都造成俄罗斯经济的动荡和脆弱。 <br />
<br />
　　4、金融秩序混乱，不良资产比重过高，本币信誉低下。 <br />
<br />
　　1)俄罗斯金融业并不发达，但金融机构却林立丛生。目前俄罗斯约有商业银行1800家，其中半数以上其资产总额在15万美元以下。这些商业银行主要从事高息揽存和金融市场的投机活动。现在俄罗斯商业银行的呆帐约占其资产总额比例的50%，尽管商业银行的资产有保险机制保护，但保险公司的实际保付能力估计也只能达到应付资产的50%左右。 <br />
<br />
　　2)俄罗斯中央银行作为唯一货币发行机关，不具有超然独立地位，不能有效履行货币调控和金融监管的职责。主要表现在三个方面：一是在政府的干预下靠大量增发货币来维持财政开支和救助商业银行。二是调控方式不灵活。三是具有明显的盈利倾向。 <br />
<br />
　　3)俄罗斯企业和居民对本国金融机构和本币不信任。据估计，1994-1997年俄罗斯吸引外资共计580亿美元，但同期企业和居民流出国内的资金却高达670亿美元；现在俄罗斯居民手持外汇资金高达400-500亿美元，根本不往本国银行里存。 <br />
<br />
　　4)企业相互之间的拖欠款。拖欠资金已成为俄罗斯经济运行的一大特点，政府和银行对此束手无策。同时，由于俄罗斯金融市场，特别是短期债券和拆借市场的收益率水平太高，一般为50%-200%，致使企业根本不进行实物资产的投资，有钱就购买短期债券或委托拆借投资。1996年俄罗斯企业对固定资产的投资下降了18%，1997 年又下降了5%，1998年上半年则更进一步下降了10%。 <br />
<br />
　　5)金融犯罪活动猖獗，尤其是企业和银行相互勾结，违法违规操作，形成影子经济和地下经济，使投融资活动的投机性和风险性加大。 <br />
<br />
　　5、政局动荡。 <br />
<br />
　　俄罗斯政局动荡由来已久。1998年3月俄罗斯总统提名年仅35岁的基里延科为总理，遭到议会下院的强烈反对。虽然总统强制提名和议会下院三次表决勉强通过，但政府与议会之间的政治斗争重新激化。在新一轮政治格斗的时刻，政府提出的经济改革方案和摆脱财政金融困境的大政方针，肯定会遭到议会的强烈反对或拖延表决。基里延科下台，并未根本解决政府与议会之间的政治矛盾，这对解决日益深重的金融危机极为不利。? <br />
<br />
　　政府与议会之间的政治矛盾不断激化，政治危机与金融危机交织在一起，使金融形势更为恶化。 <br />
<br />
[编辑]俄罗斯金融危机的官方对策 <br />
　　面对上述不断发生的金融市场动荡，基政府当时采取的对策主要有以下三条。 <br />
<br />
　　首先，保卢布，办法是提高利率。央行将贴现率由5月19日的30%不断上调至5月27日的150%。短短8天，提高了4倍。6月4日起曾降至60%，但不久又上调至110%。同时抛售美元干预汇率，外汇储备由年初的200亿美元减少到150亿美元。 <br />
<br />
　　其次，由举借内债转向举借外债。俄从1993年到1998年5月通过发行国债来弥补财政赤字，但代价很大。据报道，财政从市场每筹1卢布资金就要花费 12卢布的代价，而国际金融市场上筹资的利息一般无如此之高，故而俄于1996年11月起大规模发行欧洲债券，并已筹得约45亿美元，1998年拟再发行 60亿欧洲债券。1998年7月13日又从IMF为首的西方大国金融机构借到226个亿美元的贷款。 <br />
<br />
　　第三，延长整个债务的偿还期，以缓解还债高峰。俄当时的内外债务总额不算高，还未超过GDP的44%。其主要问题是还债集中，短期债务缺乏偿债能力，当时俄债务构成中绝大多数是借期不到一年的短债，近三年间将发生严重的债务危机。由于3～4月间的政府危机，增加了居民对政府的不信任感，购买国债大量减少。1998年4月俄发行国债不到200亿卢布，而当月还本付息高达367亿卢布，借新债已抵不上还旧债，财政更加紧张。当时测算，1998年下半年内每月需归还310亿卢布，如新的国债乏人购买，税收又困难，则还债额要超过国家月收入的40%。因此必须改变还债的期限结构，用借长期新债来归还短期旧债，以错开还债高峰。IMF原承诺年内将分3期先提供148亿美元，无疑对俄推迟还债高峰起一定作用。原以为，俄这次金融危机由于IMF和西方大国出手支援可能得以缓解，至少能稳定半年。殊不料，不到一个月，于8月中又爆发了更为严重的第三次金融大波动，而且导致基里延科新政府的垮台。 <br />
<br />
　　这次金融大波动的直接诱因是由于基政府贸然推行三项强硬的稳定金融措施，导致投资者对政府的信心丧失，叶利钦再次临阵换马，更加剧了危机的严重程度。 <br />
<br />
　　8月1日基政府推出稳定金融的经济纲领，投资者对其能否产生预期效果，信心不足。因社会经济形势已相当严峻，政府的增收节支措施难以立竿见影，反映在债券上外资不愿购买俄有价证券，相反还抛售手中的证券。俄报称为“黑色星期一”的8月10日那天，世界证券市场上，原苏联欠外国商业银行的旧债券的价格跌至面值的36%，俄新发行的欧洲债券只值一半。8月11日，俄国内证券市场的短期国债券收益率激增至100%。俄政府为增强投资者信心，对8月12日到期的国债进行清偿。财政部将7月13日从IMF得到的48亿美元贷款中拨出10亿美元用于清偿，余下38亿美元增加外汇储备。原以为当天付出的53亿卢布中会有一部分再购债券而回笼。殊不知，债民不但未购新债券，还将大部分清偿款用于购进美元，其余则或撤出市场，或留在手中以待时机。第二天(8月13日)国际大炒家索罗斯在报刊上公然敦促俄政府卢布贬值15%～25%。当天，俄国际文传电讯社计算的100种工业股票价格指数大跌，跌到仅及年初的26%, 跌掉74%。若干外资银行预期卢布贬值，纷纷要求俄银行提前还贷。在这期间美国的标准-蒲耳氏计算机统计服务公司和穆迪氏投资服务公司都宣布降低对俄外债以及俄主要银行和大工业集团的信誉评估等级。同时，7月份税收只征收到120亿卢布，而执行预算每月不少于200亿卢布，缺口很大。在这些内外压力下，政府惊慌失措，不知如何应对。眼看国债券又将陆续到期，年底前政府需偿还内外债240亿美元，而当时外汇储备仅为170亿美元，不够还债，更难以干预外汇市场。政府在此内外交困形势下，就贸然决定，于8月17日推出了三项强硬的应急措施，即：第一项，扩大卢布汇率浮动区间，调低卢布汇率的上限到9.5∶1。这实际上是将卢布兑美元的汇率由6.295贬至9.5，贬值50%以上，市场有此预期，以后卢布汇率必然大跌，10天内跌到20~21∶1，将稳定了3年多的卢布汇率一下冲垮了。 <br />
<br />
　　第二项延期90天偿还到期的外债，估计有150亿美元。 <br />
<br />
　　第三项转换内债偿还期，将1999年12月31日前到期的价值达200亿美元的国债转换成3、4、5年期限的中期国债。在转换结束前，国债市场暂停交易。 <br />
<br />
　　这三项措施一公布，立即引起舆论大哗，股票大跌而停摆，卢布汇率猛跌，后来，央行干脆宣布任由卢布自由浮动，老百姓挤提卢布以兑进美元，或者抢购消费品。卢布汇率失守，股市更是一泻千里。到8月28日，俄国际文传电讯社综合指数所包括的100种股票的市价已跌至159.2亿美元，比年初的1033.56亿美元，下跌85%，后来干脆停业，变成一文不值。 <br />
<br />
[编辑]俄罗斯金融危机的后果 <br />
　　俄罗斯这次金融大风波带来的后果十分严重。不仅使本国已是困难重重的经济雪上加霜，还震撼了全球金融市场。 <br />
<br />
　　1、国内居民存款损失一半。 <br />
<br />
　　进口商品价格上涨2～3倍，国产品也连带成倍上涨。9月份，消费物价上升40%，超过1992年2月的上升36%，成为转型以来的最高。居民实际工资收入下降13.8%，近1/3的居民处于贫困线以下。整个经济下降5%，工业--5.2%，农业--10%，外贸--16.1%. <br />
<br />
　　2、批商业银行，尤其是大银行损失惨重。 <br />
<br />
　　西方报刊已惊呼“俄罗斯金融寡头们的没落”。它们前期为牟取利差，曾大量借取利率较低的外债，估计共约 300亿美元，兑为卢布后，购进高回报率的国债券。现在卢布贬值，国债券又要由高利、短期转换为低利、长期，里外损失巨大。仅金融七巨头之一的sbs-农业银行当时就握有相当于10亿美元的国家短期债券，顷刻之间不值几文。据估计，商业银行中有一半濒临破产。俄罗斯的sbs-农业银行和国际商业银行已被暂时置于中央银行管理之下，其余几家大银行不得不将自己的商业账户转移到俄罗斯储蓄银行。由于普里马科夫出任总理，组成中左政府，金融7巨头与政治关系基本被割断，势力大为削弱。 <br />
<br />
　　3、俄罗斯金融危机波及欧美、拉美，形成全球效应。 <br />
<br />
　　本来，俄国经济经过连续6年下降，在世界经济中已微不足道，它的GDP仅占全球的不到2%。俄金融市场规模也很小。到1997年股市最兴旺的8月的日成交额也不过1亿美元。这在国际金融市场中也无足轻重。但是，为什么这样一个配角可撼动全球经济，引发欧美发达国家的惊慌呢？这里有三个原因。 <br />
<br />
　　1）由于从1997年7月到1998年8月新兴市场国家的金融市场几乎都相继出了问题，使国际投资者对新兴市场的可靠性产生怀疑，纷纷撤资避险，形成连锁反应，俄罗斯当然也在其中。8月俄罗斯金融市场崩溃后，9月上旬短短两周内，巴西也出现外资撤走140亿美元、外汇存底从700亿美元减到500亿美元的金融危机。下次将轮到谁已成为国际金融市场上的关注热点。 <br />
<br />
　　2）美国对冲基金染指俄罗斯金融市场，并在俄8月17日那次汇市大跌、股市崩盘、国债停市中遭到了巨大损失。这是造成全球效应的一个最主要原因。现已获悉，外国投资者在俄那次金融大风波中约损失330亿美元，其中美国长期资本管理公司(即对冲基金)亏损25 亿美元，索罗斯量子对冲基金亏损20亿美元，美国银行家信托公司亏损4.88亿美元，现已面临被德意志银行合并的可能。现行的对冲基金借巨资进行金融投机，一旦失利，提供贷款的银行即闻讯逼债，甚至惊动政府出面救急(如美政府对长期资本管理公司的救助)，由原来的“暗箱”操作暴露在光天化日之下，引起多方面惊恐，形成连锁反应。 <br />
<br />
　　3）德国是俄罗斯的最大债权国，俄罗斯出现由金融危机引起的信用危机很快波及德国乃至欧洲。俄迄今已欠德国750亿马克(约合444亿美元)，其中主要是政府担保的银行贷款。俄罗斯金融市场一有风吹草动，就影响德国债权人的安危，因此引起震动。其冲击波也传到了欧洲金融市场，如法兰克福股市上的DAX比价曾一度下跌3%；巴黎股市的CAC40指数下跌1.76%；阿姆斯特丹股市下跌2%；苏黎世股市下跌1.6%，等等。 <br />
<br />
　　俄罗斯金融危机把俄罗斯经济推向深渊，1998年经济下降已成定局，估计1999年经济还将下降。这是坏事，但这也可能促使俄转变经济改革和发展道路，抛弃货币主义一套作法，成为俄绝处逢生的转机。从普里马科夫3个多月来的作为看，只要以后政局稳定，是有可能出现转机的。 <br />
