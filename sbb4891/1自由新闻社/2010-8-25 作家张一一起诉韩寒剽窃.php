																											<div id="postmessage_796812" class="t_msgfont">张一一欲用裸奔打动李湘说明什么？<br />
<br />
2006年12月05日 09:27:47 　来源：人民网<br />
<br />
据报道，向李湘求婚的青年作家张一一称，“如果李湘还不答应嫁给我的话，我将在她足迹所至的31个大中城市裸奔，以纪念湘湘已然不再的31载的美丽芳华。”“我也是没有办法，不得已而为之，谁叫她老不回复我啊，我这样做，叫一个‘爱之弥深，念之弥切’。”<br />
<br />
80后作家张一一，是土生土长的湖南人，是“看湘湘的节目长大的”，自称自小对李湘有一种特别的感情。从小对明星有仰慕之情也不为过，在李湘刚刚结束一段不幸福的婚姻，正是需要关怀和温暖的时候，表达自己的爱慕之情也无可非议。但是在遇到李湘的婉言拒绝之后，还揪着不放，就有点说不过去，特别是声称要裸奔，不仅是一时一处的裸奔，而是要在李湘足迹所至的31个大中城市裸奔，真是痴迷。<br />
<br />
婚姻是个大事，是两厢情愿的促成，“落花有意，流水无情”肯定是难以成双。作家张纵然就是才华横溢，才高八斗，也不能说想娶谁就是谁，想要谁就是谁。李湘在发给此情迷的短信中，已经说得再明白不过了，两条理由婉拒：一是年龄不合适，二是已经有了意中人，不论此理由是否真实，李不同意张的追求是肯定的，本来此事也就到此为止，可作家张偏不就此收心罢休，还以“你千万别把我不当回事儿，我的一狗仔朋友业已拍到了万国城小区阿伦家中你们俩的亲密照片，小心我现在就给你公布出来！还有，你在北京的几处住所我也早已摸得清清楚楚，如果你再不回答我，我只好去你家门口蹲着等你了！你看着办吧。”用这样的言语进行威胁。这就有点与其作家的温文而雅的面孔相悖，更多了些死皮赖脸。<br />
<br />
追求婚姻是个人的自由，拒绝婚姻也是个人的自由，心有灵犀才能一点通，任何一方的回绝都不可能结合，任何一方的苦思苦想只能是徒劳。当然女方不同意，男方继续追着不放，现实中也不乏其人，也不失为有成功之例。但是用过激行为，用恫吓的语气只能是事与愿违。<br />
<br />
“裸”这一字眼是隐私的关键，当今一些人动不动就用裸来说事，用裸来出名，这种“裸”文化越演越烈，好像不裸就不能抬高自己的身价，不裸就不能表白内心世界。裸其实是一种最简单，最粗俗的下作之举，它不需要什么技艺，也不需要耗费多少精力，一脱了知。但是如果人人都“裸”，这世界将是什么世界，这种原始人群的还朴，在某种程度上是一种倒退，是一种污染。作家张说要裸奔31个城市，你就是裸奔全世界那又怎么样？如果用裸能够解决问题，还要大脑干嘛，还要智力干嘛，这同其它动物有何区别。<br />
<br />
本来地上的路千万条，东方不亮西方亮，咬住一点不放，这也太痴情，太死板。凭作家张的条件，何需强人所难，何需一条路走到黑，何须做出有失身份的裸。你纵然就是“裸”遍大江南北，只能成为人们观赏的怪人，不能得到的仍然不能得到，到头来可能失去的更多。<br />
<br />
80后作家张一一的裸似乎有惯性，2005年底，文学青年李索伦为出版自己的小说《非此非彼》在北京西单图书大厦裸奔的“伟大创举”，便系张一一此君一手策划。李索伦因“扰乱社会公共秩序”被拘之后，张一一曾写下《当文学不再裸奔》和《我与李索伦不得不说的故事》等文章为李索伦的裸奔行为辩护，在文坛乃至社会各界引起强烈反响。裸为该作家确实带来了些名气，但这种方式带来的名气，失掉的是人格，失去了作家应有的廉耻。这一次作家张是否还想用同样的方式来出名？如果是以此事，以此举来出名，笔者认为万万使不得，其躯体肯定没有什么特色，人们已经厌了、烦了，到时只能落得个神经有毛病的骂名。（罗瑞明）</div>

							
							
							
							
