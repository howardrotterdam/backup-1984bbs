<br />
　　经济部主任冯启若辞职；<br />
<br />
　　《南方周末》城市版主任陈菊红已去美国哈佛大学学习；<br />
<br />
　　谢方伟担任即将出炉的《新知时报》副主编；<br />
<br />
　　专栏编辑拉家渡干脆回家休养，传闻去《羊城晚报&#8226;新闻周刊》担任编务总监；<br />
<br />
　　头版编辑方三文、消费广场编辑曹西洪、城市版编辑甄茜已经递交辞职报告；<br />
<br />
　　记者李玉宵，杨海鹏，翟明磊，贾明，朱强，杜卫东，江华，贾明，周浩已经明确离职意向……<br />
<br />
　　一位辞职者说：“我们是雅典的公民，但现在是斯巴达人的臣民。”<br />
