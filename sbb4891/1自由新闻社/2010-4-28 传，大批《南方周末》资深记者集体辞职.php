													<h2>传，大批《南方周末》资深记者集体辞职</h2>
						
						
																											<div id="postmessage_587610" class="t_msgfont">　<a href="http://uhome.duk.cn/space.php?uid=50&amp;do=blog&amp;id=402" target="_blank">http://uhome.duk.cn/space.php?uid=50&amp;do=blog&amp;id=402</a><br />
<img src="http://uhome.duk.cn/attachment/201004/28/50_1272438610MQgm.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
天下没有不散的宴席，在一周前珠海开了全体会议后，因对新任主编，南方集团派来的向熹独裁作风不满，《南方周末》十名左右资深记者向报社递交辞职报告。这些记者是《南方周末》辉煌时代仅存的一批人马，知情者相信，随他们的出走，饱受弹压，苦难深重的南方周末，将沦为一张普通的小报。一位辞职者说：“我们是雅典的公民，但现在是斯巴达人的臣民。”<br />
<br />
　　《南方周末》城市版主编陈菊红，头版编辑方三文，消费广场主编曹西洪已经递交辞职报告，记者李玉宵，杨海鹏，翟明磊，贾明，朱强，杜卫东，甄茜，江华，贾明，周浩已经明确离职意向。<br />
<br />
　　都是向熹惹的祸。让一个在体制外毫无经验的干部，负责一个体制外的媒体，这是《南方日报》集团领导用人的失策。向熹此人纸上谈兵可以，但为人浮夸，不够坦诚，《南方周末》的城市版投入大，收益小，挤压原版面的广告资源，是典型的样板工程。向熹曾在三年前欲到《南方周末》当记者，为江艺平所拒，理由是向熹与南报记者气质上有差异。《南方日报》的老人都会记得几年前的“七一晚会”，向熹朗诵一歌颂党的诗，未罢居然泣不成声，给人以大伪之徒的感觉。他那一套在体制内行得通，但在《南方周末》行不通。除非对南报彻底换血。问题是南报彻底换了血，魂魄将不复存在。<br />
<br />
　　南报的内部冲突，实际上是体制内外两种文化的冲突。这次事件中，率先倒戈一击的是上海记者站的两个记者，杨海鹏和翟明垒，他们批评南报新任领导在内部事务中起码的知情权，居然对记者降薪一半这种大事，事先不沟通，事后不解释。在上海站对此质询时，也不予理会。按党报传统，这种事无须多解释；但按《南方周末》的传统，碰到如此大事，领导层会对大家说明，寻求谅解。向熹年纪虽轻，但一直深受体制内文化的浸润，自然不知南报的“理路”，把权力的分量看得太重，而轻视下层的反应。在翟杨公开批评后，南报资深编辑曹西弘深受刺激，此人一向在周末有清誉，在能力操守上无可指责，他的行动是上书《南方日报》老总，要求撤换能力及理念无法与《南方周末》报契合的向熹。但《南方日报》处理此事者，主观臆断曹是“非组织活动”，要求曹及附议的翟明垒，杨海鹏道歉，遂使人们对《南方日报》彻底绝望。询问他们的人揣测，他们是想把向熹拉下马，背后有《南方周末》前领导的主使。但事实上，他们没有证据，甚至在三人（翟杨的信是给曹的私信）的信中，观点也有很大差异，罗织罪名非常困难。何况以私人信件治人处分，于理于法不通，遂使向熹步入僵局。处分难，大家会不服；不处分也难领导威信自然扫地，他的强权会被瓦解。但让他道歉，以向熹的自负，他是不能错的。于是，因为此三人（俱为记者中的憨货）而拉开的伤口，就不会愈合，南方周末就只有失血了。对《南方日报》来说，调换向熹恐怕是最得人心的选择。向熹与《南方周末》的记者，本不是同类，硬凑在一起，冲突不可避免。即便当局者，也不会想到记者们的反应会这么快，如不及时处置，《南方周末》几个月精华尽去，不是什么假设。<br />
<br />
　　《南方日报》的自信，在于有源源不断的人才投奔在去年最受大学毕业生欢迎的中国境内企业中，《南方日报》排行三十六位。因此，他们对那些《南方周末》主张自身权力的资深记者编辑，态度简慢。虽然他们理应知道，南方报业集团的荣誉，很大程度是由这些合同制记者编辑创造的。在总体经济环境不好的情况下，他们更多会顾及内部职工的利益和保障，《南方周末》的合同制记者（多是广东以外投奔南报的一流人才），人均收入十几万的高成本，在南方报业集团中，遂成众矢之的。在强敌已灭（《羊城晚报》境遇不佳，《广州日报》因为领导层腐败案元气大伤），狡兔死走狗烹，也是一个必然选择。降低劳动成本，招揽起薪底的新人，或许成了一个大的方向。向熹只是忠实执行命令者，这场风波的背后，《南方日报》的经济环境和广州媒体的竞争减弱，恐怕是个重要原因。<br />
<br />
　　任何报纸都有她的生命周期。《南方周末》红了十几年，对中国的报纸已经是奇迹了。今年全国广告爆跌，广东媒体更是惨不忍睹。《南方周末》虽然有足够财力，维持原有薪酬标准，问题是她有赡养父母责任，而且要求发展如城市版，要大量投入。这就为她布置了重重障碍。向熹一手策划的城市版，定位不准，格调与《南方周末》读者期望距离很大，也为他的被攻击制造了口实。向熹理应明白，他不是沈颢，不是程益中，也不是刘洲伟，即便龚小跃，李戎，连清川，也胜他好几分。一位南方周末的大姐说，以向熹的判断力，写作采访能力，如果参加南报公开招考，十有八九不会被录用，他如何能让《南方周末》的记者心服呢？她评价向氏用了“味道不对”四个字。如果用GOOGLE搜向熹的文章，难有一篇符合《南方周末》的标准。<br />
<br />
　　《南方周末》最近在珠海开会，新任主编向熹在会上公布了一个决定，让一些周末老记者倒吸一口凉气。向熹说：由于近期网络上对本报不实攻击很多，为保护《南方周末》品牌，命令新组建的“品牌工作室”收集网上文章，对恶意批评，将组织以“网友”身份回击。记者们对这种愚蠢的做法议论纷纷。如何界定“ 恶意”和“善意”？一张大报，有些恶意批评，也属平常，何必在意？何必专门安排人手，鬼鬼祟祟搞匿名“回应”？ 这与“网特”何异？<br />
<br />
　　《西祠胡同》斑竹“牛吃草”，五个月前已归化《南方周末》，在进入南报后，他被要求控制那些对《南方周末》批评的帖子，这令他十分难堪。<br />
<br />
　　《南方周末》的记者队伍不稳定，近期又遇大规模出走。向熹的无知和独裁，使《南方周末》处境岌岌可危。钳制网上非议，是他粉饰太平工程的一部分。<br />
<br />
　　SB，这是记者们对这位从某报空降到《南方周末》的年轻主编的评价。<br />
<br />
　　但向熹对他的这个“愚不可及”之举，好象甚为得意。<br />
<br />
