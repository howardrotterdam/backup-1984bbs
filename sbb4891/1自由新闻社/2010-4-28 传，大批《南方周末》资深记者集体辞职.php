													<h2>南方报业传媒集团就记者集体辞职传言发表声明</h2>
						
						
																											<div id="postmessage_587631" class="t_msgfont"><a href="http://www.news.zj.com/china/detail/458593.html" target="_blank">http://www.news.zj.com/china/detail/458593.html</a><br />
来源：南方报业传媒集团 发布日期：2005-09-01<br />
<br />
中新浙江网9月1日电 最近，一篇题为“《南方周末》报资深记者集体辞职”的网文被人有目的地在互联网上广为散布。鉴于该文已严重侵害《南方周末》及其有关负责人的名誉，南方报业传媒集团法律事务部声明如下：<br />
<br />
　　一、该文及跟帖中提到的16位资深记者辞职一事严重失实。据调查核实，该文最早出现在3年前，内容严重扭曲了事实真相；而跟帖中进一步将原文中的10个人夸大为16人，更是严重失实。<br />
<br />
　　据查，该文与跟帖提到的16人中，2002年7月中旬南方周末报社在珠海召开采编人员业务会议后离职的为曹西弘、杜卫东、杨海鹏、甄茜等4人。<br />
<br />
　　该文及跟帖中提到的其他人员有关情况如下：<br />
<br />
　　江艺平长期在本集团领导班子中工作，是现任集团管委会委员；2000年1月未再担任《南方周末》主编职务；<br />
<br />
　　钱刚2001年8月调南方日报新闻采编中心任副主任；2003年8月调动到上海大学工作；<br />
<br />
　　谢方伟的辞职时间为2001年10月；<br />
<br />
　　陈菊红是在珠海会议前因赴哈佛大学学习而离职；拉家渡在珠海会议前已确定到某媒体担任领导职务；<br />
<br />
　　方三文、李玉宵、周浩于2002年9月调到本集团的另一份报纸《21世纪经济报道》工作；而冯启若是2004年10月才由《南方周末》调到《21世纪经济报道》工作，所谓当时辞职纯属子虚乌有；<br />
<br />
　　贾明和翟明磊的辞职时间分别为2003年3月和2003年9月。<br />
<br />
　　朱强直至现在仍担任《南方周末》上海区域中心主任；江华在本集团2004年4月创办新媒体《南方人物周刊》(属南方周末报系)时才从《南方周末》抽调，参与新刊物的筹备等工作。<br />
