　　大杨网站近期贴出了一段宣传视频，在视频中亮相的是伯克希尔&#8226;哈撒韦公司(Berkshire Hathaway)董事长兼首席执行长巴菲特。这段视频给李桂莲的公司带来了显著提振。巴菲特在视频中对李桂莲、大杨以及自己的九套创世西装大加赞赏。他说：“我现在的九套西服都是中国制造，我把其余的西服都扔掉了。”<br />
