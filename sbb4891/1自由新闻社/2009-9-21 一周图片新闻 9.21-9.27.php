													<h2>杭州富二代飙车撞人</h2>
						
						
																											<div id="postmessage_252849" class="t_msgfont">今年5月，杭州的一位富家子弟在街头飙车时撞死了一位普通出身的年轻人后，这起事件在这个繁华的东部都市点燃了阶层对立的火焰。<br />
.<br />
<br />
<br />
<br />
<br />
<br />
.<br />
<br />
<br />
<br />
<br />
.<br />
<br />
　　<img src="http://chinese.wsj.com/pictures/photo/FatalCrosswalk/01.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　Shai Oster/The Wall Street Journal <br />
<br />
　　25岁的谭卓是一名电信工程师，老家在偏远的乡镇，下岗的父母好不容易才供他上完大学。5月7日谭卓在过马路时被一辆跑车撞死。事隔数月之后，谭卓的母亲仍然不愿提起儿子，整日以泪洗面。<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://chinese.wsj.com/pictures/photo/FatalCrosswalk/02.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　Josh Chin for The Wall Street Journal <br />
<br />
　　在遇难之前，谭卓的人生经历可谓现代中国的成功典范──贫穷出身的人可以通过勤奋学习和努力工作来改变命运，成为一名受人羡慕的白领。图为谭卓大学时的照片。<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://chinese.wsj.com/pictures/photo/FatalCrosswalk/03.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　Shai Oster/The Wall Street Journal <br />
<br />
　　而那位撞死谭卓的肇事者――20岁的胡斌，他的人生与谭卓的成长过经历形成了鲜明对比。胡斌生在一个富商家庭，家中拥有多辆轿车和并在杭州风景秀丽的黄金地段拥有多套房产。图为谭卓的卧室照片，他死后这里依旧保持着原样。<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://chinese.wsj.com/pictures/photo/FatalCrosswalk/04.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　Sue Feng/The Wall Street Journal <br />
<br />
　　车祸当晚，事故现场很快便围满了旁观者和闻讯赶来的记者。胡斌的几个朋友也赶来安慰他。在警察和救护车赶到时，胡斌坐在车内用手挡着脸，他的朋友则在附近抽烟嬉笑。<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://chinese.wsj.com/pictures/photo/FatalCrosswalk/05.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　Shai Oster/The Wall Street Journal <br />
<br />
　　当地一份报纸在事发第二天以“富家子弟把马路当F1赛道”为标题报导了此事。当载有谭卓遗体的灵车出殡时，有一千多人在路上给他送葬。当地警方当晚发表声明，承诺将彻底调查此事。<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://chinese.wsj.com/pictures/photo/FatalCrosswalk/06.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　Shai Oster/The Wall Street Journal <br />
<br />
　　谭卓的父母打开一个黑色手提箱，里面装着儿子在学校获得的一些奖状、儿子的驾照、党员证和几张照片。这些天来，他们很少把箱子打开，以免再想起丧子之痛。<br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://chinese.wsj.com/pictures/photo/FatalCrosswalk/07.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　Josh Chin for The Wall Street Journal <br />
<br />
　　谭卓的父亲谭跃又高又瘦，而谭卓的眼睛长得跟他父亲一模一样。谭跃说这起交通事故之所以引起人们的高度关注，是因为政府在调查中的错误做法。他说，杭州市民非常生气这些富家子在公路上飙车，出了事后他们还试图遮掩，这让老百姓更加愤怒。<br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://chinese.wsj.com/pictures/photo/FatalCrosswalk/08.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　Shai Oster/The Wall Street Journal <br />
<br />
　　在7月中旬，法院以“交通肇事罪”判处胡斌三年有期徒刑之后，公众的愤怒再次被点燃，人们普遍认为这一判决过于宽大。胡斌的家庭给谭跃夫妇113万元人民币（合165,000千美元）作为赔偿。图为一栋简陋的三层水泥小楼，这里是谭卓成长的地方。<br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://chinese.wsj.com/pictures/photo/FatalCrosswalk/09.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　Wang Dingxu/Xinhua <br />
<br />
　　胡斌在法庭上受审时的照片。因为比肇事时胖了不少，而且照片上也看不到他胳膊上的特殊的伤疤，以致于后来出现了“胡斌家找了个替身”的谣言。<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://chinese.wsj.com/pictures/photo/FatalCrosswalk/10.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　Josh Chin for The Wall Street Journal <br />
<br />
　　在杭州，有关部门承诺打击飙车，甚至还在市里的人行横道上画上了大大的心形图案，以督促司机安全驾驶。就在离潭卓出事地点的不远处，首个特殊的人行横道线――黄色的“爱心线”在11天后画成。<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://chinese.wsj.com/pictures/photo/FatalCrosswalk/11.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　Josh Chin for The Wall Street Journal <br />
<br />
　　不过就在胡斌被判刑几周之后，在距离出事地点不远的一条爱心斑马线上，一名外地来杭务工的少年马芳芳被一辆保时捷SUV撞死，司机28岁。死者21岁的男友戴旺朝（音）说，你说有钱人的孩子为什么会这样做？因为他们认为自己不会受到惩罚。他还说，假如是我撞了人，我肯定要坐很多年的牢。</div>

							
							
							
							
