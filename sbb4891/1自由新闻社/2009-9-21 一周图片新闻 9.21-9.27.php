													<h2>巴菲特爱穿的中国西装</h2>
						
						
																											<div id="postmessage_257612" class="t_msgfont">　　美国最著名的资本家和中国共产党最高领导人之间或许并没有太多的共同之处，不过巴菲特和Hu Jintao似乎都爱穿同一品牌的西装。<br />
.<br />
