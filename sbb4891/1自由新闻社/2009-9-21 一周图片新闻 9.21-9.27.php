<br />
　　“创世”，这个还不太为人知的男装品牌是由大连大杨企业集团创立的。今年63岁的李桂莲在中国东北的大连市开创了这家服装公司，这个个子不高的农村妇女如今已经成为了时尚界的大亨。<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://chinese.wsj.com/pictures/photo/20090924b/02.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
