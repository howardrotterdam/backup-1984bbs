																											<div id="postmessage_259794" class="t_msgfont">茅于轼：房价上涨过快与富人没地方花钱只能买房有关<br />
2009年09月27日 楚天金报<br />
“中国经济百年论坛”昨在本集团隆重开讲 <br />
<br />
国内外顶级学者湖畔“论道” <br />
<br />
昨日，由湖北日报参与主办的“中国经济百年论坛”在本集团隆重开讲。此次论坛可谓是智者的聚合，茅于轼、丁学良、铃木宏昌(日本)等多位国内外的顶级学者论道东湖之畔，回顾新中国成立六十年来的经济发展轨迹，思考与展望未来中国经济的可持续发展之路。部分学者的发言整理如下： <br />
<br />
著名经济学家茅于轼：房价上涨过快与“富人没地方花钱”有关 <br />
<br />
论坛上，茅于轼探讨了“中国的成功模式能不能持久”的问题。他认为，中国特有的“无产阶级专政+市场经济”发展模式的成功，已在实践中被证实，市场经济被证明是最有效的经济制度，而专政下的政府也具有绕过发展中国家的发展过程中瓶颈，以及提供一个稳定的宏观环境等功能。但他也认为，这种模式要可持续发展，必须同时满足两个条件：一是精英治理，因为社会已是越发复杂的系统，国家的治理者中需要更多受过高等教育的“专家”；二是治国的精英要优先考虑基层群众的利益，国家才能稳定。 <br />
<br />
在接受记者采访时，提出过“廉租房不该建厕所”的茅于轼也对当前的房地产业表达了看法。他认为，当前房市的热度是一个泡沫，产生泡沫的原因则是“在财富生产特别多的情况下，由于分配不均，富人分配很多，但却没有别的地方花，就只能去买房子”。而茅于轼认为，“富人的钱没地方花”的问题出在“我们的金融市场不好”，“把钱集中起来投到效益最高的地方去，拿钱生钱的机会非常少”。 <br />
<br />
对于未来的宏观调控，茅于轼认为应当改善金融市场，同时对于中小企业的扶持要落实。 <br />
<br />
长江学者徐勇：中国农民“不差粮，差钱!” <br />
<br />
“你耕田来我织布，我挑水来你浇园……”在昨天的论坛上，华中师范大学博导、长江学者徐勇教授引用名戏《天仙配》说，这是中国古代典型的小农经济。不过，他提到，在某种程度上可以说，正是新时代的农民，创造了中国的经济奇迹，帮中国经济顺利渡过了全球金融危机。“以前，村庄就是世界;如今，世界就是村庄(地球村)。”徐勇认为，新中国成立60年来，小农经济社会化带来巨大变革。尤其是30年来，农民工逐渐成为民营经济的主体，即使是在外企中，农民工也占较大比重。中国农民勤劳、节俭，正因为此，“是中国农民创造了经济奇迹，使中国经济顺利渡过金融危机”。 <br />
<br />
徐勇同时提到，现在的农民“不差粮，差钱，差文化!”于是大批农民进城打工。这样，21世纪谁来种田，正成为一个社会问题。</div>

							
							
							
							
