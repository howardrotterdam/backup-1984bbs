============================<br />
<br />
<strong>龙应台：两岸理解需时间 </strong><br />
明蠹r新, (20:46)<br />
<br />
作家龙应台新书「大江大海一九四九」，传出遭中国大陆官方封杀。龙应台今天首度回应事件。龙应台表示，两岸理解需要时间，等大陆有时间看完这本书後，「我相信决定是不一样的」。<br />
<br />
「大江大海一九四九」内容讲述1949年政府迁台至今的60年历史，龙应台探访多位走过战乱年代的外省丶本省老兵，希望「打开伤痛记忆的黑盒子」，抚平历史伤痛。<br />
<br />
新书还未出版前，台湾多家媒体已刊载部分内容，大陆网友转载文章内容，并在网路上讨论，近日却传出大陆官方出面，要求各入口网站删除文章。<br />
<br />
「我不着急丶没有关系丶慢慢来」，龙应台答覆中央社记者询问时说，海峡两岸相互理解需要时间，新书强调的就是温柔的力量与和平，所以不用着急。<br />
<br />
龙应台表示，现在大陆正紧锣密鼓筹备10月1日国庆日，没有时间与心情好好读这本书，她相信封杀她的当事人还没有好好认真看过这本新书，「等到他们看完，相信决定是不一样的」。<br />
<br />
龙应台表示，非常希望大陆人民能读她的新作，这可以让对岸人民看见台湾历史的伤痕，从中认识台湾，会比较深刻。<br />
（中央社）<br />
