													<h2>龙应台《大江大海一九四九》香港全球首发演讲文字稿（前49分钟）</h2>
						
						
																											<div id="postmessage_250496" class="t_msgfont">该文字稿由推友子仲整理，这是他的博客<a href="http://blog.sina.com.cn/taijicao" target="_blank">http://blog.sina.com.cn/taijicao</a><br />
<br />
<br />
&nbsp; &nbsp; 大家晚安！<br />
<br />
&nbsp; &nbsp; 在一个礼拜五的晚上从工作场所赶到这里来，中间的过程可能很辛苦。在我们真的开场之前，我其实注意到，其实让这一场演讲能够成功的推出，幕后有很多很多的工作的同仁，包括港大的……，我们的……，包括天地图书公司，还有很多致公朋友在这里，然后其中有一部分工作人员、同仁们，现在其实是看着我的背，坐在我的后头，所以我现在想说，这样子进行演讲，其实不是很舒服（转身面对身后的姑娘们）：“我可不可以建议你们找个很舒服的位置，你们就可以看到我了，而不是看到我的背。（全场轻笑）舒服的坐下来吧。”<br />
<br />
&nbsp; &nbsp; 我刚刚在后台，等候的时候。后台那个房间我是第一次去，灯很亮，有镜子，那个装饰完全是像一个演员的化妆室。我坐在那儿安静一下的时候，有一个我前面二十几年的写作从来没有过的感觉，那个感觉就是，我在后面一个安静的房间里，我可以听到前面大厅里人们进来的脚步声，以及彼此的交谈声音。在那一刻呢，我闭起眼睛的时候，感觉到我是刚刚从一个很深很深的丛林中走出来的一个人。那个层林，在过去的四百天里头，是一个又一个的战场，一场又一场的战役，之后的荒野。闪过了我的脑海里头，就有东北的战场，有淮海的战役，有无数无数的，白骨累累的，遗迹在我脑海里闪过。然后那个感觉就是说，我好像走过那些所有的荒凉的战场，然后走到今天，就走到这样一个，有美丽的舞台，繁华的灯光，然后来接受朋友的恭喜，跟读者的期待，在这样一个场合。从那个荒凉的境界走出来，然后来接受今天这样的一个繁华的场合，我心里真的有说不出的惭愧。这个是我过去二十几年来，二十几本书，二十几场新书发布会所从来没有过的感受。<br />
<br />
&nbsp; &nbsp; 为什么将《大》我选择在香港，而且是在香港大学，做全球的首发的演讲？这中间有一些思索。第一个当然是因为在我要进入这本书的一个研究，以及田野，以及写作的最开始的时候，香港大学，它竟然会有那样的创意以及那样的气魄，得到孔梁巧荣（音）女士的支持，给我创造了一个这样的环境，使得我过去的这一年，整个的可以投入这本书的工作。所以，如果要选择一个任何的地方来做我的首发演讲的话，那港大是第一选，没有任何其他的余地。只有用这种方式，可以来表达我心里的感谢。我确实也是希望，通过港大这次跟孔梁女士的合作，也可以在华文世界的高等教育里头，创立一个这样的典范：是不是有更多的大学，它可以做这样的事情，让更多不那么完全合乎大学体制的人，可以有创作跟焕发的机会。那第二个原因是：为什么选在是在香港来做这个首发，甚至于连台北也在香港之后，接下来还有马来西亚跟新加坡，还有温哥华，旧金山，等等等等，另外一个原因是，在我写完了之后，才发现，它或许是一个巧合，可是那个巧合是一个多么有意义的巧合。如果你手上有那本书，您会注意到那个封面的设计，那是一个美术编辑所做的，他第一次拿给我看的时候，我就有会心的微笑，因为，一半是蓝，一半是红。它可能代表什么呢？（全场轻笑）然后中间有裂缝。写完了之后，我突然想到，回头一想，冥冥之中竟然安排这本书是在香港创作，香港完成，那香港不就刚好是那个蓝与红之间的裂缝吗？所以在这里完成这个作品，而且在这里首发，我就觉得它完全吻合这本书的整个的历史脉络，也凸显了香港这个地方，它不见得人人都时时感觉到的，它的特殊性，它的特殊的贡献和它的特殊的历史的位置。<br />
<br />
&nbsp; &nbsp; 今天，刚刚建民已经说了，是一个有意义的日子，因为它刚好是9.18。9.18作为抗日战争的开始，跟这本书有非常非常深层的关系。然后今天在这里，陆佑堂演讲，是我的第一次，所以我想说，在我讲这本书之前，我一定要讲一下陆佑堂这个地点。今天这个时间，是一个特殊的时间，今天这个地点，是一个非常特殊的地点，我告诉你为什么。1933年的二月十三号，你们知道是谁在这个地方演讲吗？萧伯纳。那个时候他说：如果二十岁的时候，你是一个赤色的革命者，到了四十岁的时候，你还可能不会落伍；但是如果你在二十岁的时候不是一个赤色的革命者，那么到了五十岁的时候，你一定是一个不堪设想的化石。萧伯纳在这个地方讲的。往前推十年，一九二三年，谁在这个地方演讲呢？大家都知道了，孙中山先生在这里演讲。然后，在1923年的2月20号，……有一个记录，就是孙中山先生在这里演讲的状况，（英文引述）在这个地方发生的对话。……一九三五年，两年以后，胡适之博士第一次来到香港，在这一个讲台上接受了他的荣誉博士学位。我们再过几年，接着就到了42、43年的时候，战争中、兵荒马乱中，这一个厅变成了临时医院，所有的病患、伤患都躺在这个地方哀叫、呻吟，港大的学生变成临时的护士，中间有一个很有名的护士，她后来变得很有名，她叫张爱玲。那你现在可以想象在我们这个厅，都是哀号的声音、战争的景况，张爱玲所描写的这个厅，在当时，她说：病人的日子长得不耐烦，上头就派下来叫他们去捡米，除去里面的沙子，因为实在没事做，他们似乎很喜欢这单调的工作，时间一长，这些病人跟自己的伤口也发生了感情。这是张爱玲，real cool。那最后呢，我还要说到我们在的这个厅，叫做陆佑堂，陆佑，何许人也？1844年生，1917年过世，广东（我很喜欢这个地名）省江门市鹤山雅瑶镇黄洞村的人，本来姓黄，但是家里太贫穷，养不起他，把他送到一个姓陆的家庭里头去做童工，所以他就改姓陆，这个孩子后来变成马来亚的锡矿大王，橡胶大王，这是陆佑。陆佑有一个有名的儿子，非常非常喜欢电影，这个陆佑的儿子因为喜欢电影，在50年代初就创办了电影制片厂，后来开创了整个香港电影的黄金时代，电冒（音）公司，到后来变成国泰公司，这一个儿子叫什么名字呢？叫做陆韵涛（音），他成功在哪里？一个，他重金聘请最有才华的人写剧本。谁为他写剧本呢？张爱玲，为他写剧本。他同时也用心的去培养明星，他培养了哪些明星呢？我们还记得有葛兰（音），有林黛（音），有游敏（音），这都是他的，也都是陆韵涛的。接下来，1964年的时候，陆韵涛跟他的这个电影公司的主要的成员，飞到台北去出席亚太影展，陆韵涛这批人飞到台北之后，到各地走，然后接受各地当地的人的接待。当他们这批人到了花莲的时候，在花莲当地有一个中学的校长接待他，这个校长有个九岁的孩子，男孩子，所以大家一起照合照的时候，这个校长觉得跟陆韵涛这个有名的人拍合照，就跟他这个九岁的儿子说：过来过来一起拍。这个九岁的孩子就站在陆韵涛身边一起拍了这张照片，那个九岁的儿子叫做李安（全场笑声），李安还记得一起拍照的那一刻。但是，也就在同一趟旅行，陆韵涛坐飞机要到台中的时候，飞机失事了，所以机上的人全部死亡，那么他整个的电影公司，也就戛然而止。所以影评人会说，如果陆韵涛那个飞机没有失事的话，香港的电影可能会有不同的风貌出现。可是大部分人不知道的是，那架飞机并不是失事，那架飞机是被劫机，而且劫机的人是国军，而且是因为两岸的对峙，所以才会有劫机事件。讲到李安，他告诉我，跟陆韵涛照相的这个过程，是什么时候呢？是他在拍色戒的时候，他在哪里拍色戒？他在香港大学拍色戒。如果你看过色戒，其中有一幕是抗战的热血爱国青年在舞台上演话剧，你知道那个舞台在哪里吗？就是这个舞台。好，这就是今天我们在陆佑堂做这个演讲。我想大家应该知道在历史的大的幕落、小的幕落里头，我们身在何处，身在何时。<br />
<br />
&nbsp; &nbsp; 《大江大海1949》，曾经有记者问我说：龙应台你的书9月1号出版，是不是冲着10月1号来的？（全场轻笑）。我听了有点愕然，然后想，要冲着10月1号来，这个有点难吧。因为我第一次动念要写1949，是20年前，1989。1989的时候，我住在德国，大家也记得1989在20世纪是个重要的年代。在6月的时候，天安门发生了事情，11月的时候，柏林围墙崩塌。也是在同样一个时候，我们看到苏联帝国的解体。所以一九八九的时候，我身在德国，又看着这个柏林围墙的倒塌，你当然会知道，说：这个东德跟西德的分裂，以及冷战的开始，也是从1949开始的。所以，到了1989，整个的社会秩序或说是国际秩序有了新的挪动，大挪移。尤其是看到人们跨过柏林围墙，同样的一个民族分隔这么多年之后的那种痛哭流涕，我心里就在想说，同样是分裂的年代、战争的年代，你如果要来真正的写中国的一九四九的话，它精彩的程度，它惊心动魄的程度，绝对不会亚于欧洲所发生的事情。所以第一次动念是在一九八九的时候。但是中间有很多其他的事情，一晃就是十年。到了一九九九，等于是50周年到了，我心里就想，不行，我要动手了。所以我就开始有计划的去读一九四九的老报纸，包括大陆的、台湾的、香港的报纸，从一月一号开始读起。但是没有读多久，台北市长就出现了。我就跟着他回到台北去做了公务员，这个公务员一做就做了三年半将近四年。当这位台北市长再度高票当选的时候，我就比较坚定地说我退回，到我的书房里头去，就是2003年的时候。从2003年到2008年去年我真正开始进入这四百天的丛林，在私人的生活里头就发生了很多事情，包括，我的父亲过世了，我的母亲失忆了。我才发现到说，那扇历史的门，是永远永远地关上了，你再也敲不开了，有一种时间的急迫感，使得我终于在去年开始动手，所以不是冲着今年的十月一号来的。甚至于在过去的四百天里头，我敢说，一直到了可能320天，我都还没有信心，自己可以做完这本书，更何况，中间有很多的过程里头，我有一点自怨自艾，觉得自己自不量力，根本做不到这件事情，又没有人逼迫你做，如果你不做，港大也不会逼迫你说：你去死吧。（笑声）。所以，曾经在某些特别脆弱的时刻，我曾经想过，是不是，算了吧。所以，不是冲着十月一号来的。<br />
<br />
&nbsp; &nbsp; 在这个写作的过程里头，我不是一个历史学家，我只是一个历史的学生，甚至于我是一个历史的小学生。所以，在前面的80%的时间，全部都是史料的扒梳，只是带着一个非常大的问号，想把某些事情搞懂。而且我心里还有一个习惯上的、性格上的执着，我不太相信大家都相信的东西。当大家多说一九四九是什么样子的时候，我心里想说，它也许不是那样的。当你带着这种问号去的时候，进入一个丛林，你就完了，你就惨了。为什么？如同小红帽进入大兴安岭，我不知道大兴安岭有多大，只是说在台湾长大的小孩，从小学的是中国地图，然后大兴安岭四个字在我心中有无限的想象，以为是世界上最深最大的森林。那今天是不是，我完全不知道，我又没有去过。但是那个巨大，在我心中有个非常非常清晰的形象，有一种一个小孩子去到大兴安岭去，要找出一条路来那样的艰难。我的第一个田野是在2008年的8月1号，在去年七月中的时候，……里头所收藏的蒋介石先生的日记，49年前后的，它是陆续陆续一段一段的打开的。就是在七月中的时候，蒋介石先生哪一段日记才刚刚打开，你可以去看了。所以我第一个田野就是飞到……去看他的日记。到了那里发现，来自世界各地的历史学家都到齐了，都在那里。每一个人都像小学生一样，背上书包，早上八点半，你就站到那个玻璃门前面去等，等它开门然后你进去。他们像小学生的话，我就像幼稚园大班生，背着书包跟在他们后面，然后鱼贯而入，你必须把所有的东西全部放到旁边锁起来，因为你不能够照相，你不能够复印，你不能用任何的机器，你甚至不能带自己的笔进去抄，你必须用那个里面留给你的笔跟纸。在那里看蒋先生日记看了十五天之后，我回到香港，我决定说，这本书我不写大人物。大人物不是我的主角，反而小人物才是。那这个大人物包括政治人物，他也包括大知识分子，或者是影响最重大的人。看了蒋先生日记之后，我反而决定从最小最小的叙事去看待那个宏大的历史。我心里有非常清楚的知道，我要写的不是一本学术的著作，我不要写一本学术的著作。第一个原因是：我只是历史的小学生，我本来就没有资格去写一个历史的学术著作。第二个，在内战，或者说1949，大分裂之后的60年的这一个关卡，我自己觉得所需要的这个书，不是再多一本学术著作，而是这样的一个作品，它最有历史的基础，可是它无论如何要有一种文学的魅力，要有文学的像蜻蜓一样的翅膀，会把那个非常非常遥远的，生硬的，沉重的，无法消化的历史，可以用那个蜻蜓一样的翅膀，可以飞出学者的书房，而进入最普通最普通，平常都不太愿意进书店买一本书的人的心里头。所以从一开始，我心里就知道说，好，我希望去下最艰难的历史功夫，但是到最后，这必须是一本文学的作品。所以这个是大家在最开始的时候就知道的事情。然后就是一个探索之旅或者是探险之旅，因为当我进入丛林的时候，我一进去就看到了，千万条的羊肠小径，每一条小径一走进去，一条岔路马上出现了。岔路出现你就要决定走左边走右边还是往上面走，但是你永远不知道会到哪里去。这个四百天，恐怕到了三分之二的时候，我还在那个丛林里头非常的焦虑，因为我不知道我找的东西在哪里，还不知道那条线在那里，还不知道我到底会从这个丛林的哪一个口出来。所以打开这本书的时候你会看到，第一页的扉页写的是：向所有被时代践踏、侮辱、伤害的人致敬，以及后面的一首诗。这一个扉页的完成其实是甚至于十五万字全部都写完了之后，回头再让自己消化一下，才浮出来的一个东西，写到最后一秒种。<br />
<br />
&nbsp; &nbsp; 在我跟外界的接触里头，几乎每一个接触对我都是一个大大小小的撞击跟碰撞，我在台湾，当有人听说我在写1949的时候，台湾人的第一个反应就是说，噢，你要写外省老兵，我心里想说，不是啊，不对啊。可是你讲不清楚，无法解释。当我不在台湾，在香港，或者是见到大陆的朋友的时候，当他听说龙应台要写一九四九，他的第一个反应就是：噢，你要写建国六十年。（笑）。我毕竟还是一个台湾长大的小孩，我一想：啊，建国六十年，我连想都没想到这件事情。（笑）。所以这个红跟蓝，他的这个基础真的是非常不一样。但是这个两边的密集反应都让我大吃一惊、吓一跳，促使我真正的去想，那你到底在找什么东西。<br />
<br />
&nbsp; &nbsp; 我的第一个发现是，如果您看了这本书的前面第一部，它完全是家族史。你会看到美君，就是我的母亲，24岁的浙江淳安的年轻女孩、女郎，她在那一种兵荒马乱、难民如潮水一样从一艘又一艘的逃难船涌上了码头，然后大雨倾盆，她手里抱着一个婴儿，丈夫不知道去向，不知何去何从，连一个遮雨的棚子都没有的那种境况之下。我确实是希望去好好的了解一下我的父亲跟母亲、那个一九四九年，颠沛流离到台湾的这些人、这两百万人他们到底是怎么一回事，单纯的，我就想把他们的历史搞清楚。但是，我第一个发现是说：慢一点，这两百万人（其实没有两百万人，120万到150万，我们姑且说是两百万人），哗一下像洪水一样来到了这个岛。可是这个岛在当时本身就有六百万人，你可以看到现在，就是说任何一个国家有大量的移民进来的时候，当地的人都是非常恐慌的，有各种可怕的事情会发生。我现在回想那个时候，六百万人本来安安静静住在这里，然后你突然来两百万人，然后这两百万人告诉你说，我是你的统治者，对不起。那种撞击会不大吗？不可能，一定非常非常的巨大。更何况，这从天而降的两百万人，是刚刚跟日本这个国家打了八年的仗，生死相斗。可是在地的那个六百万人，在过去的五十年，已经过了三代人，是读日本书，讲日本话，而且是认为自己是日本人，而且是为日本这个国家以及它的军队去效劳的人，等于是两边的死敌。就是说那两百万从天而降的、潮水一样涌进来的人不是普通的……。他们除了是难民之外，而且他们的语言你也听不懂，而且他本来是你的敌国的人。这两股人在1949年的时候，就这样突然在码头上撞击了，我怎么可能只去了解我的父母也就是那两百万人想些什么东西，而不去了解那在地的六百万人心里头在想什么事情？有这样一个第一个发现和撞击之后，才使得我为了了解一九四九，我必须往前推，走向一九四五，而更往前推要走到44，43，42，是这个原因。因此到后面这个书不是大家心里所以为的那个一九四九的写法。<br />
<br />
&nbsp; &nbsp; 如果现在回头去看这本书的话，带着一点文学的眼光去看，你就会发现说，这本书有三个地方都像一本书的开始。第一个地方是写1945的上海，当日本战败以后，第七舰队，44艘军舰，进入黄埔港的那个大的镜头，那个很像一个电影的开场。我试过一个那样的开场，后来想想说，不满意。有第二个开场，是台北市的街道图，它就完全是中国地图，从东北一直到西南，所以台北人是每天走在一个兵棋推演图上面的。走了多少年？走了六十年。那是第二个开场。以那个开场的结构呢，那个书稿，已经送到印刷厂去了，8月4号，已经排版排完了，到了8月8号的时候，我的台北的好朋友们为我庆祝“出狱”，庆祝“出狱”的那个晚餐，大家七嘴八舌，一直在谈论这本书。饭吃完以后，我想，大事不好，我去把那个书稿要回来，已经排好的版毁掉，重新来起。到最后重新来起，你现在看到的版，就是从家族史，从美君，从槐生，从家族史进入。那你要问我说，为什么会有这么巨大的改变？因为在第二版到第三版的中间，我又发现，我想要触及的那些年轻人，他们竟然连“孙立人”三个字都不知道怎么写，当他们读到“王百涛（音）”将军这三个字的时候，他们问我王百涛是共军还是国军。当讲到徐蚌会战的时候，他不知道是哪个徐，哪个蚌，或者哪个淮，哪个海。讲到说上海1948年的通货膨胀，那个法币如何如何的时候，他会说：法币，那个时候中国人为什么要用法国钱？我猜想，不行，我可能要更温柔的带读者进入，这本书有这样一个过程。所以后来才有从家族史进入，从家族史进入后，我第二圈是我的同代人，这个同代人里边，包括小人物，也包括大人物。大人物里头譬如说，里头有马英九，也有萧万长等。可是他们在里头，不是因为他们是大人物，而是因为他们只不过是我的同代人而已，就是说，我还活着，他也活着，同一个时代而已。然后再扩展从家族史到同代人到国族史，然后最后的那一圈，说几个同心人，最后那一圈是世界史，看到德国跟苏联的关系，做一个……。<br />
<br />
&nbsp; &nbsp; 我今天想跟大家分享一些的呢，是我做田野的这些过程，家族史的部分，刚才大家在影片里头已经看到一点点，有时候我在国立基学院的写作室里头工作到凌晨两三点的时候，那个地方静得有点可怕。所以到工作的后半段的时候，有人开始说，国立基有没有鬼故事，我说，我不要听，你不要告诉我（笑）！我不管它有没有喔。家族史的部分，大家已经有一点点感觉了。在国立基学院，深夜里一个人工作的时候，到六月之前，还可以听到杜鹃叫，杜鹃叫起来，你没有伤心的事，你也想哭。我常常有时候半夜里会吓到，自从开始写这本书之后，我回屏东去看妈妈的时间都减少了，然后有时候会吓一跳就是说，如果因为写这本书忽略了给妈妈的时间，然后你书写完了之后，妈妈如果不在的话，你是不是会后悔，你根本就做的这一件事情。所以，有这样一个过程。<br />
<br />
&nbsp; &nbsp; ……</div>

							
							
							
							
