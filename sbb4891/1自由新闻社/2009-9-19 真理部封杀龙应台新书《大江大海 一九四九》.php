													<h2>龙应台新作打开记忆黑盒子</h2>
						
						
																											<div id="postmessage_250579" class="t_msgfont">中国 | 2009.09.19 <br />
1949年对台海两岸是历史上重要的一年，国民政府迁台，中华人民共和国建国，形成两岸分治的局面。在今年届满一甲子的时间点，作家龙应台最近发表新作《大江大海一九四九》，以许多小人物为主角，打开这段华人记忆e的黑匣子。龙应台希望，这本书能促进台湾内部的了解，让有些创伤，能够温柔的被治疗。 <br />
<br />
酝酿了十年，闭关400天，寻访了台湾的大街小巷、也走遍了中国的大江南北，作家龙应台的新书《大江大海一九四九》，以诚实的态度，重新回顾1949年国民政府兵败撤离到台湾，改变全球华人命运的年代，打开这段华人记忆e的黑盒子。<br />
<br />
龙应台说，自己是带著深情与谦卑在写这本书，她要以文字来向父母那一代人致敬和告别，更要以文学的方式来面对这段执政者害怕面对的历史。她希望提醒现代年轻人，由於这些长辈们经历的痛苦，我们现在才能享受和平的生活。龙应台说：&quot;面对那个大的历史，面对长辈他们所经过跌倒的地方，每个坑、每次流血的地方，我心e有非常谦卑的感觉，就是我们在和平长大的感觉，你显得特别优雅、开阔、国际视野，其实全部都是因为前面有这些人，他们曾经经过什么样的痛苦，给了我们这个和平的土壤。&quot;<br />
<br />
从自己家族的故事说起，龙应台寻访了十一位历史见证者，从他们的眼光，架构出这段历史书e不曾写过的历史。书里的主角，有在新几内亚战俘营幸存的游击队长、有参与接收台湾的士兵、还有被抓去大陆参加国共内战，后来变成了解放军的台湾原住民。另外，台湾总统马英九的母亲秦厚修、作家白先勇、企业家林百里等人的故事也都成了龙应台笔下的生动题材。<br />
<br />
龙应台说，外省人1949年来到台湾和本省人共同生活，组成命运共同体，但彼此却完全不知道对方的情感脉络，不懂对方的伤痛；她认为，有些创伤即使过了60年，记忆的黑盒子还是需要被打开、被治疗，才能避免伤痛转化成政治上的斗争：&quot;你其实60年来从没问过身边那个人，他心里最痛的地方是在哪里。我们竟然凑合着60年，而没有真正把整个边缘化的盖子打开过，我想说的是，你心里的伤痛，如果没有打开过，它永远不会治疗好，然后它就会恶化，转化成政治斗争。&quot;<br />
<br />
1949对国民政府来讲，是一段不堪回首的战败历史。不过，龙应台却说，她以身为失败者的第二代为荣，正因为军事和政治上的失败，才造就了台湾六十年来展新的文明价值。龙应台表示，战争没有胜利者，在中国大陆即将庆祝建政60周年、也是国民政府迁台60周年之际，她写这本书，希望提醒某些人不要再有战败心理症候群，要面对、记忆60年来光荣的、值得尊敬的部分，跳脱历史党派争斗，给曾经为国牺牲的人应得的、迟来的尊敬。她甚至提醒，中国领导人Hu Jintao也应该对人民说声&quot;对不起&quot;，对经历战乱的人民展现疼惜。<br />
<br />
一九四九年，是一个重要的时代印记，在今年届满一甲子的时间点，除了龙应台的《大江大海一九四九》外，台湾以&quot;一九四九&quot;为主题的新书颇为多元，有的检视一九四九年以来两岸内部的变化、中美台关系的演变，以及政治舞台上来去的人物；有的邀请不同领域、世代和文化背景的人物，以第一人称的纪实笔调，书写他们的生命故事；有的则着墨于国民政府迁台的经过。<br />
<br />
德国之声台北特约记者尼尔<br />
责编：苗子</div>

							
							
							
							
