													<h2>【上海书评】穿行于大江与大海之上的……历史叙事与观念（评《大江大海一九四九》）</h2>
						
						
																											<div id="postmessage_251092" class="t_msgfont"><a href="http://www.dfdaily.com/node2/node31/node2433/userobject1ai190410.shtml" target="_blank">http://www.dfdaily.com/node2/nod ... ject1ai190410.shtml</a><br />
李公明<br />
<br />
　　龙应台的新书《大江大海一九四九》（台湾天下杂志股份有限公司，2009年8月）似乎又要卷起一股“龙旋风”，但这回龙女士自言她关注的是创伤、“隐忍不言的伤”，询问的是“你，痛在什么地方？”虽然直接切割着历史年轮中最敏感的剖面，虽然似乎是随手抖落的卷宗残页重如磐石，但她自言是文学、是致敬、是温热的治疗。以龙女士的文字修为，要做到温热的煽情实在不难：“所有的颠沛流离，最后都由大江走向大海；所有的生离死别，都发生在某一个车站、码头。上了船，就是一生。”这很像一部历史大片的片头独白或广告语，画面上是急湍的江流或苍茫的大海。但如果仅仅是这样的，它就真的只是如龙女士自己所讲的“做一个文学的告别”，而且还文学得有点浮热。其实它真正的重心是历史，由口述、文献和家族记忆等材料编织的历史记忆。这是《大江大海》真正的价值所在。<br />
<br />
　　既然是写历史，不管用的是什么笔法，历史观的存在是很自然的，尽管未必是有意识的建构。《大江大海》的历史观是什么？好像没有人这么问作者，但是这个问题不是没有意义的。开卷即扑面而来的是穿行于大江大海之上、逃离于战火战场之外的逃难人群，也就是我姥姥从小对我们说的“走难”(粤语)。逃难不仅制造了无数的生离死别，而且制造了人口的变化、文化的变迁、社会的改观等等巨变，但是在传统的历史学研究中的逃难更多的只是战争或灾害的产物。《大江大海》对逃难人群的悲悯与抚慰的确有很煽情的力量，然而以历史观念作为审察逃难史的研究意识未够明确，自古以来就有的对于离乱难民的伤时忧难之思时常遮盖了对历史情境的精准洞察。当然，书中也有例外的闪光，如采访诗人痖弦的一段（第94-95页），是很好的逃难史个案的开端。《大江大海》有很感人的逃难史的感性波澜，假如能更深入地触及逃难史的研究观念和个案研究，某种意义上的“逃难史观”便会自然呈现。与逃难主题相连的是失败者主题：一是“战争，有‘胜利者’吗？”二是“我，以身为‘失败者’的下一代为荣”，因为“正是他们，以‘失败’教导了我们，什么才是真正值得追求的价值”。那么，似乎可以说，“失败史观”在这里已经呼之欲出。至于以和平主义反对战争，这也可以是一种历史观，属于人道主义的历史观。总之，无论历史的大江如何水流湍急，无论历史的大海是如何的渊面幽深，历史观与历史叙事的关系都是值得探讨的。<br />
<br />
　　历史研究的入口，首先是要掌握所研究领域的资料，而资料的掌握既需要有“上穷碧落下黄泉”的勇气和毅力，也离不开各种工具书的引领。如王森教授所言，如王国维、陈垣等前辈学人多有重视工具书的编篡，一是为自己研究之需，二是惠及后来的学者。颜娟英主编的《上海美术风云：1872-1949申报艺术资料条目索引》（台湾“中央研究院”历史语言研究所，2006年6月）是近年来出现的二十世纪中国美术史研究的重要工具书，所提供的资讯途径不仅仅是资料指引的路标，而且同时它本身就构成了中国在现代性进程中发生的艺术与社会空间结构互动的动态网路图，它本身就是以选择资讯为特征的研究成果。《申报》由英人创办，立足于上海租界，其长达七十八年的生存均以广告与发行的收入为据，所呈现的中国现代社会之丰富资讯罕有其他媒体可以相比。作为中国现代美术活动的中心汇聚地之一的上海，其美术教育、画家迹历、画坛展事、艺术市场、商业美术与设计之发展等等方面，无不折射出中国现代美术的转型与建构的历程。因此，在近年来的二十世纪中国美术研究中，以上海美术为中心的研究课题不断增加。《申报》所登载过的所有与艺术相关的资料自然成为了有待不断开垦的宝库，《上海美术风云》就是进入这一宝库的地图和路标。主编者在本书的“解题”中对《申报》所提供资讯与现代美术研究的种种重要联系作了扼要的评述，可以发现，无论是新式美术教育还是画坛艺事，《申报》的广告、报道所记叙的事实有很多是学校、展览会本身所保存的资料中没有记载的，其史料价值的重要性不言而喻。书中附录的两篇论文《图画与手工――中国近代艺术教育的诞生》（吴方正）、《不息的变动――以上海美术学校为中心的美术教育运动》（颜娟英）的论述对象刚好在时间上前后相继，而后者就是运用《申报》资料的重要成果，当然也并不局限于《申报》的资料。对报纸的内容作资料索引，既要处理分类的问题，同时如何把该则资讯的原标题及核心内容准确地表述出来，也非易事。在“解题”和“凡例”中可见本书的处理方式以严谨、便于检索为原则，颇为得当。<br />
<br />
　　社会进步与社会政策密切相关，但是在社会政策的制定过程中又往往受到更多因素的制约，如何使社会政策成为社会进步的助力和标识，对处于社会转型期的国家尤为重要。我们应该特别重视的是，有关社会政策的知识和思考应该成为现代公民启蒙教育的重要组成部分，哈特利・迪安的《社会政策学十讲》（岳经纶等译，上海人民出版社，2009年6月）作为一本社会政策学的普及读物，其内容和表述方式大可借鉴。有点出乎想象的是，作者在英文版序言中强调的是对这一研究领域的“激情”：在为一个贫困社区争取权益的斗争中，对于制度失效和无能的愤怒导致作者回到学术界研究“社会政策学”。社会政策学研究的是增进人类福祉所必需的社会关系，但在现实中人们最直觉的理解可能是解决社会问题。这种理解有其合理性，因为两者之间的确有密切关系，但是社会政策学只是为解决社会问题提供解读和批判各种社会决策、社会问题的思想能力。这正是公民教育中的容易被忽视的要义，特别是相对于普法教育而言，“普社教育”的缺失尚未引起重视。仅以我在近来参与的有关广州医保制度新政与医院博弈的困境、经济适用房的制度设计对低收入者的价格歧视、电梯时代的公共责任等等问题的讨论来看，关于“社会支出”、“福利体制”、“公共责任”等社会政策的基本概念和理性思辨还远未成为公共舆论的共识；尤其是在相关利益部门的自我陈述或辩解中，对社会政策中的价值问题的漠视、曲解甚至敌意比比皆是。本书作者所讲的那种为了争取贫困社区利益而与政府办事部门和“街头官僚”作斗争过程所感到的愤怒，我也是时常在感受着。然而，对于非专业研究者而言，我们不必返回书斋，而是在书斋与社会之间架设桥梁，使社会政策学的价值、概念、方法成为促使社会政策进步的思想资源。</div>

							
							
							
							
