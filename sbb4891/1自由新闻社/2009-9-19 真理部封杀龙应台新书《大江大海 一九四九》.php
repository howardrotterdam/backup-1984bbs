													<h2>真理部封杀龙应台新书《大江大海 一九四九》</h2>
						
						
																											<div id="postmessage_249112" class="t_msgfont">2009/09/18 15时24分53秒 <br />
文u钱钢香港大学新闻及传媒研究中心中国传媒研究计划主任（香港经济日报-评论） <br />
<br />
上个周末开始，旅居本港的著名作家龙应台的文章，在内地互联网遭封杀。9月11日，北京各网站接到北京市网管办通过质谱下达的禁令：删除全部龙应台的文章，并不能再发她的文章。 <br />
<br />
新书报导内地网只剩标题 <br />
<br />
网管办禁令下达前，最新有关龙应台的新闻，是新华网9月3日发出的“龙应台新书追溯60年沧桑世事讲述平凡人曲折命运。”报道所指“新书”，即龙应台的“大江大海一九四九”。现在，在不同网站还可以查到这篇报道的标题，但在新华网打开此文，会看到：“抱歉！您查看的是已删除或过期的稿件“，在腾讯网打开，看到的是：”页面没有找到，5秒钟之后将带您进入首页！“登录到原先存有龙应台文库的网站，龙文踪影全无。 <br />
<br />
网管效率高指令直达编辑 <br />
<br />
北京市网管办的全称是“北京市互联网宣传管理办公室”，其上级机构是国务院新闻办公室网络宣传管理局。网管，在内地的传媒管制上正扮演愈来愈重要的角色。他们使用先进的技术监控网络表现，用不同的即时通讯工具实行快捷，直接的“以一对众”的扁平化管理，分分钟将指令下达到网站新闻编辑的电脑上，手机上。 <br />
<br />
在网站密集的北京，北京市网管办正预备推出实名制度，并正筹组“监控不良网站动向”的万人网络志愿者团队。和网管相比，传统的传媒警察-中宣部和各级宣传部的阅评组，已成低效率的老牛破车。 <br />
<br />
龙应台是当代台湾知识分子中对中国大陆最有影响的一位，早在上世纪80年代，她的“中国人，你为什么不生气”（即“野火集”）就曾风靡内地。上世纪90年代后期，她为“南方周末”等媒体撰文，10年来，有近百篇文章在内地媒体刊出。她的一些著作在内地出版，今年6月，“亲爱的安德烈”入选新浪中国好书榜半年榜，排名第二。 <br />
<br />
宣传部门多年来对龙应台很头痛。龙文传达的观念，对正统说教是猛烈的冲击。但她善于和管制者博弈，从社会，文化等不同角度切入，文字富有魅力，读者喜爱，检查官无奈。2005年，内地网络转载“，为台湾民主辩护”，“中国青年报”“冰点”周刊先后发表的“你可能不知道的台湾”，“文化是什么？”和“一个主席的三鞠躬“，被认为是龙应台在大陆的四个冲击波。2006年初，屡次发表龙文而使当局不悦的”冰点“，因为发表学者袁伟时谈义和团的文章而遭整肃。龙应台愤而发表“请用文明说服我-致Hu Jintao的公开信”，谴责舆论控制。 <br />
<br />
这封公开信在内地网络被封杀，但仍有媒体敢发表龙应台的文章。“冰点”事件后，她有若干重要的评论在“南方都市报”刊出。如对电影“窃听风暴”的评论：“你是有选择的”，评论陈水扁弊案的“我怎么上陈水扁这一课”，评论马英九特支费案的“路走得宽阔，人显得从容”等。 <br />
<br />
她的一些文章在内地较为敏感，传媒在发表时不无风险。如“我们记得谁？ -献给冷战中所有的牺牲者”，记述当年国民党空军派往内地执行任务的飞行员的故事。这篇文章，已经触及了“大江大海一九四九”一书的核心命题：尊重个人价值，珍惜遭到战争机器碾压的每一个无辜而不幸的生命。 <br />
<br />
封杀前新书不合国庆旋律 <br />
<br />
近期为什么全面封杀龙应台？网管办的理由外人无从得窥，但显然与国庆60周年庆典将至有关。8月初，北京市网管办已经召集各大网站负责人开会，进行国庆宣传的动员。要把网站的思想统一到宣传共产党好，社会主义好的“主旋律”上来。这是中共宣传部门的老招术，管束媒体“要帮忙，不要添乱”，杜绝“杂音，噪音”。 <br />
<br />
对他们的“主旋律”而言，“大江大海一九四九”无疑是高分贝的“杂音，噪音。”因为龙应台想打开历史的黑盒子，正视60年前残酷的真相，还坦言“以失败者的下一代为荣。“她说：”远离了1949年的价值观，我们的上一代逐渐发展出温柔的，文明的价值观，所以我们应该对他们充满深情的致谢和无尽的感恩，起点就是，说一声深深的对不起。“ <br />
<br />
敏感10月过去禁令不撤自销 <br />
<br />
在中国大陆，意识形态事实上已经淡化。今天的内地官员，早已不是马列主义的信徒，他们的行为，每每出于现实政治考量。60年庆典的宣传，当局的核心诉求，是中共的地位和国家的统一。于是涉及1949年的历史，他们不可避免要“走钢丝” -决不能否定解放战争，也不要刺激当今的台湾国民党当局。“大江大海一九四九”，势必引发网络热议，红，蓝，绿各种倾向的反应和意见将沸沸扬扬，这是管制者绝不愿看到的。不过，网站是否从此不能再发龙应台的文章？笔者存疑。眼前可能只是一次特定时段的封杀，过了敏感的10月，禁令也许会不撤自销。</div>

							
							
							
							
