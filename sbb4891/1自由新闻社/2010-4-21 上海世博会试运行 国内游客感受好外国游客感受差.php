													<h2>上海世博会试运行 国内游客感受好外国游客感受差</h2>
						
						
																											<div id="postmessage_575366" class="t_msgfont">以下均为转载<br />
<a href="http://blog.renren.com/blog/242198722/460004478" target="_blank">http://blog.renren.com/blog/242198722/460004478</a><br />
<br />
首先说一下，我就是想发些牢骚，没什么水准，看到的就当看个热闹，反正我们就是爱凑热闹嘛。早几年前我就一直很期待世博会，但真没想过有机会去现场看。前几天我爸跟我说搞到票的时候那个兴奋...昨天在宾馆里看了一晚上关于历届世博会的资料和纪录片。我的票算是“综合演练票” 我也有幸成为第一批进入世博园的人。预料到第一天开放按时去是不明智的，我就多睡了一小时，到达园区已是10点左右，上海这几天一直下雨还雾蒙蒙的，不过园区也大的足够吓唬人。在经过并不严密的安检后（我外衣口袋里的打火机没被查出）我就满心欢喜的向中国馆进发了<br />
<br />
（这个被我称作痰盂）<br />
<img src="http://fmn.xnimg.cn/fmn040/20100421/0035/b_large_7AoG_3c7000013b6d2d10.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
当我往前走了没多远时，失望开始了，好吧，我知道是第一天，我知道中国人多，可这也太夸张了，中国馆门口的队伍迂回而下足有近千人。我脑子里一下跳出了南方公园某一集的画面，我深刻体会到Cartman的酸楚，可是我没钱像他那样把世博园买下来，当然这也不可能卖。今天是星期一，这些人都不用上班的吗..今天发放的票据说是20万张，我肯定园内的人远不止这个数，况且只是浦东这的就那么多。作为一个中国人，在经过短暂的震惊后迅速回过神来，哈哈，这有什么，不就是人多点吗。但我随即又想到，这是世博会，全世界的人都会来，于是我开始四处张望，希望发现一些不同于黄色的皮肤。顺着人海，我觉得人流还不够贴切，我来到东南亚场馆的聚集地，这些场馆中除了泰国馆开放其他都是关闭的，有的甚至还是“中空”，譬如新加坡馆...<br />
<br />
（由近到远依次是买来西亚馆 新加坡馆和澳大利亚馆，马来馆对面是泰国缅甸等一些东南亚国家。故事的高潮就发生在澳大利亚馆）<br />
<br />
<img src="http://fmn.xnimg.cn/fmn042/20100421/0050/b_large_L7DS_7f9600013cc62d0d.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
我去过泰国，所以不想也用不着跟那么多人挤进泰国馆，况且挤也不一定挤的进去。<br />
<br />
这附近也就一个澳洲馆是开放的，我就下定决心排在了长长的队伍中，好歹来一趟一定得进一个馆看看。在等了差不多一个小时多一点的时候，这时我还在队伍的中间偏后，突然队伍的最前端传来一阵骚动，我远远望去看到本来只开了一个小门的入口不知道被谁把所有门都打开了，紧接着就是如同海啸一般的人从四处向澳洲馆飞速涌来，一个个跟百米冲刺一样，本来排的还算有序的队伍也顷刻间混乱起来，我心里想着 靠 大事不妙 先闪再说，迅速往馆外的广场遁去。当我抵达一处安全地带再回头看去时，只见澳洲馆外仅有的五六名澳洲工作人员慌乱的互相喊着，其中有一个儿高的年轻小伙扯破嗓子用蹩脚的中文向人海喊着：请走！请走！人太多！危险！危险！请走！这不喊倒好，一喊更乱套了，更多的人像看猴戏一样朝那白人小伙那靠拢，我老远就从他的脸上看到了惊恐，呵，他这一辈子见到的人恐怕还没现在一分钟多吧。越来越多的人拿出相机对着他一阵狂拍，他不知如何是好只能抓起身边分流人群的铁柱子试图阻挡住不断涌向场馆大门的人群。<br />
<br />
<img src="http://fmn.xnimg.cn/fmn043/20100421/0115/b_large_NyNP_1a8a0003908e2d14.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<img src="http://fmn.xnimg.cn/fmn041/20100421/0115/b_large_Qgz4_1a8a000390c42d14.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
说到这，我已经不想再描述世博园里的什么场景了，一整天，一个馆都没有进去，让我描述也描述不出来什么东西，撇开脏乱的地面和公共设施不谈，光是混乱的组织就说明上海还没有准备好，中国也没有准备好，政府似乎没有意识到，世博会最重要的不是庞大的园区面积和华丽的建筑。中国加入联合国，那是政治的交流，北京申办奥运会，是体育的交流，那世博是什么，当然是文化的交流。中国是有五千年的文化没错，但当一个老外第一次接触中国的时候，他对这个国家的第一个感受不可能就是五千年的底蕴，他是从这个国家的最重要的组成部分，人民身上去发现去探索去感悟的，我看过很多国外拍的关于中国的纪录片，他们的主要讲述对象相当一部分都是中国人的日常生活行为举止，外国人似乎更喜欢去了解这些能让他们很快得出结论的东西，那些历史那些建筑那些外在的他们也感兴趣，但从中国人身上，他们能得到更多，更多这个社会真实的一面。康有为百年梦世博，现在百年过去，中国真的迎来了世博，可是中国真的准备好了吗？当然没有，康有为也许是没有料到抗日战争，没有料到文化大革命...这些年，中国发展的很快，太快了，不应该这么快，城市的发展和人民思想的发展从一开始就没有成过正比，说的俗点，就是大家常说的那句话，人民素质不够。等到中国人素质够的那天，才是举办世博会的正确时间，那我也来梦一下，再过个几十年，如果有场战争对中国反而是好事，我们这一代人需要经历战争的洗礼。（这个观点今天不谈）然后经历了战争，以中国的力量，不用愁战后复兴，到了又一个一百年后，希望那时候的中国将拥有足够的能力再举办一次真正的世博会。<br />
