据悉，特区政府还将接着举行多场简报会、咨询会，广开渠道，充分听取社会各界及广大居民对23条立法的意见。<br />
<br />
《澳门基本法》第23条规定:“澳门特别行政区应自行立法禁止任何叛国、分裂国家、煽动叛乱、颠覆中央人民政府及窃取国家机密的行为，<strong>禁止外国的政治性组织或团体在澳门特别行政区进行政治活动，禁止澳门特别行政区的政治性组织或团体与外国的政治性组织或团体建立联系。”</strong><br />
<br />
早在2002年下半年，澳门社会已就23条立法有过广泛讨论及形成较高共识，但因一些外部因素而暂缓。行政长官曾明确表示基本法第23条要不要立法不存在讨论空间，在其第二任期内一定会完成这项重大工作。<strong>在特区政府公布的2007至2009年度公共行政改革路线图中，也把“跟进《基本法》第23条工作”列为重点内容</strong>。</div>

							
							
							
							
