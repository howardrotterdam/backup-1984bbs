2008年10月20日 10:48中国新闻网<br />
<br />
中新网10月20日电 据澳门日报报道，澳门特区政府即将提出《澳门基本法》第23条的立法草案。行政长官何厚铧周三晚将与行政会委员、立法会议员、澳区全国人大代表及政协委员、主要社团负责人座谈，公布有关的立法计划及咨询安排。<br />
