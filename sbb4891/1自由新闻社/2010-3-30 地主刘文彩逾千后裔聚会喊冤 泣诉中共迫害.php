													<h2>地主刘文彩逾千后裔聚会喊冤 泣诉中共迫害</h2>
						
						
																											<div id="postmessage_540529" class="t_msgfont">苹果日报 2010年03月30日<br />
刘文彩被批斗　逾千后裔聚会喊冤<br />
地主后人泣诉：中共血腥迫害<br />
<br />
死后仍被中共当样板批斗的四川大地主刘文彩，其逾千后人近日在家乡聚会。刘文彩孙子刘小飞谈及过去几十年，祖辈及后代因被株连而遭受不公待遇时，哽咽泣诉说：「那是血腥的迫害！」<br />
<br />
四川大邑县刘氏宗亲大聚会上周二（本月 23日）在安仁镇举行。大地主刘文彩的家族后人从全国各地和海外回到安仁镇刘氏公馆，举行空前的宗亲「清明会」。大小车辆汇集，写着宗亲聚会的红色横幅悬挂，内设 75桌宴席的大棚绵延百米，赴会者逾千，有 200多人因无法入座离去。<br />
<br />
当地镇政府为活动提供场所和音响设备，还放电影助兴。内地大批记者闻讯前往采访，四川在线、青岛新闻网等网站更实时图文并茂报道。但所有报道只提「刘氏宗亲聚会」，闭口不提聚会对历史的控诉。<br />
<br />
宗亲聚会发起人是刘文彩的孙子刘小飞。他接受美国之音访问时表示，多年来自己一直为爷爷刘文彩平反奔走，并进行家族史调查，联络刘氏家族。叙述中他情绪激动，几次哽咽说：「因为我在调查刘文彩，也就是我爷爷的情况。我要把历史公诸于众。把我们受到的伤害告诉世界。」<br />
<br />
<img src="http://pic1984.12hp.de//up/1269964099.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
被诬蔑为残酷「恶霸」家财尽失<br />
<br />
刘小飞续说：「过去几十年，我们遭到迫害，那是血腥的迫害啊！我一定要告诉全世界，我们受的那些苦，那些冤。」他感谢镇政府协助，但对当局在刘文彩庄园仍保留「揭露刘文彩罪恶展览」大表不满：「那些假东西还摆在那里，误导民众。」他指，过去几十年，族人因担心被指认同剥削，个个远离刘文彩，现在的情况不但说明族人对刘氏先人的认同，更是为几十年来先人遭到不公待遇而鸣冤。<br />
<br />
过去几年来，年逾 60岁的刘小飞一直以各种方式，驳斥中共当局对其爷爷刘文彩历史的诬蔑，包括去年 9月中国在德国展出以刘文彩为原形的大型泥塑《收租院》时，刘小飞在网上公开抗议，指那些泥塑没有根据。在刘氏庄园展览现场，他不时去向参观者揭露真相，指摘当局造假历史欺世。<br />
<br />
刘文彩是上世纪三、四十年代大邑县一个地主，中共建政后搞阶级斗争，将他塑造为「地主恶霸」典型，不但夺其财产，还罗列其「残酷剥削」种种作为。 1980年代开始，中共虽不再划分阶级成份，但刘文彩大地主的负面形象一直未消除。<br />
<br />
美国之音/本报记者<br />
<br />
四大地主被塑造成恶霸<br />
<br />
<img src="http://imgur.com/BPP8r.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
中共建政后曾力主「阶级斗争」，塑造了四大「恶霸地主」典型，分别是四川的刘文彩、东北的「周扒皮」、华北的黄世仁和海南岛的「南霸天」，以激起民愤。但近年这些形象备受质疑，刘文彩和「周扒皮」原型人物的后人更叫冤，要为先人平反。<br />
<br />
「周扒皮」是中共军旅作家高玉宝 1960年代创作的传记小说中的地主，他为了令手下雇工早起干活，半夜钻进鸡笼学鸡叫。去年，「周扒皮」原型人物的曾外孙孟令骞出书，指高玉宝传记I造事实，诋毁其先人。<br />
<br />
「黄世仁」是样板戏《白毛女》中的地主，放高利贷、年三十晚上门逼债、强霸民女为妾，民女被迫逃上深山变白发魔女；「南霸天」则是家喻户晓的芭蕾舞剧《红色娘子军》中的海南岛恶霸，后被中共枪毙。<br />
<br />
半个世纪后历史真相渐显，原来这些「恶霸地主」纯属子虚乌有的政治虚构。无怪乎刘文彩的孙子刘小飞说：「今天该是戳穿那些谎言，还历史清白的时候了！」本报记者<br />
<br />
<img src="http://pic1984.12hp.de//up/1269964121.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
刘文彩弟投共获任高职<br />
<br />
多年来，刘文彩的孙子刘小飞一直在寻找氏族后人。他透露，由于政治原因，刘氏后人以做学问居多，少有商贾，鲜有从政。<br />
<br />
据介绍，刘文彩共有六兄弟，其中又以他和弟弟刘文辉最出名。刘文彩务农，刘文辉从军。刘文辉与刘氏家族另一名人刘湘同为大军阀，控制四川省。 1949年中共军队入川，刘文辉向中共投降，获任国家农业部长高职，与家人同享官方礼遇。<br />
<br />
其时，刘文彩已病故，但死后仍遭冤屈，中共不但将刘家财产充公，还将家园辟为「阶级斗争」展览馆，还I造一个「收租院」泥塑馆，展示他是如何「残酷迫害农民」。<br />
<br />
刘小飞透露， 1949年中共军队入川时，奶奶欲带他们到海外去，但被刘文辉劝止。 1973年他的父亲曾到北京探望刘文辉，谈到家人受迫害，刘文辉流着泪说：「老二，我对不起你们，早知道这样，当年我就带你们走了！」本报记者</div>

							
							
							
							
