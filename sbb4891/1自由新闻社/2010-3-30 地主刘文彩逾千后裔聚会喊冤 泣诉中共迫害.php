中国大陆40岁以上的人很少有不知道刘文彩和收租院的故事。2009年法兰克福书展期间，法兰克福街头出现中国大型雕塑展览收租院的海报。收租院雕刻展飘洋过海到德国，是为了配合2009年秋天法兰克福书展中国主宾国活动。但是一些中国独立作家却对这种做法提出异议。在法兰克福书展期间，本台驻柏林特约记者丹兰和我共同采访了独立作家戴晴。戴晴手拿四川作家笑蜀写的《大地主刘文彩》。于是我们请她谈谈收租院和刘文彩。<br />
<br />
戴晴表示，收租院是一个为毛泽东阶级斗争需要，而造假编出来的谎话。她作为了解这段历史的人，义不容辞要把历史的真实与当局展览的动机，向法兰克福观众们做出解释。<br />
<br />
戴晴认为，中国政府现在将“收租院”拿到法兰克福展览的动机，是要向世界显示自己合法性虽然不来自选举，却来自人民的感情这一主旋律。但收租院创作的每个步骤，都是由中共宣传部门操控的，完全是一个宣传品而不是艺术品。<br />
<br />
戴晴表示，把这么大一个谎话带到法兰克福来，却不带作家笑蜀通过深入调查写出的《大地主刘文彩》，令人遗憾。<br />
<br />
关于刘文彩的家世，戴晴说，刘文彩并不是靠收租致富的，他最初是靠武装保护贩毒发的家，然后变成一个回馈社会的工商业和土地经营者，绰号叫“刘善人”。他在大邑县建的“文采中学”是当地少有的好中学。<br />
<br />
刘文彩1948年患肺病死后被厚葬，10年后，因“阶级斗争”需要，他又被“扬尸刨坟”。看守坟墓的贫苦农民阻拦不住，当场气死，被装进刘文彩的好棺材里 “因祸得福”。最有意思的是刘文彩的六弟刘文辉。刘文辉1949年以前是川康实力派军阀。当年蒋介石撤退时，本想获得他的支持，没想到，他一直是周恩来的统战对象，与共产党关系密切。他在关键时刻起义，为中共战胜蒋介石立下大功。然而，在毛泽东阶级斗争的宣传需要时，中共绝不念旧情，照样诬陷他的家族，以致他伤心至极，后半生无法还乡，临终前悲痛得“哭不出声来”。<br />
<br />
