																											<div id="postmessage_83294" class="t_msgfont">中新网4月29日报道 中共中央政治局委员、重庆市委书记薄熙来今天向重庆市民发出一条手机短信，将自己喜欢的句子与民众分享。继“唱红歌，读经典，讲故事”后，薄在渝又掀起一股文化思想新潮，意用“红段子”引导新舆论。<br />
<br />
二十八日上午，薄熙来在短信中这样写道：“我很喜欢毛主席的几句话：‘世界是我们的，做事要大家来’，‘世界上怕就怕认真二字，共产党就最讲认真’，‘人是需要有点精神的’,这些话很精干，很实在，也很提气。”<br />
<br />
薄熙来此举，为当地举行的红色短信创作传播大赛拉开了序幕。这场名为“红言颂”的活动，鼓励重庆官员百姓传送、转发“红色短信”。薄称，红色短信意指积极、健康、向上的短信，既包括体现共产党红色精神的“红言”，也指古今中外的名人名言、格言警句和市民自创的符合中华传统美德、社会公德的励志箴言。<br />
<br />
在当下手机已从单纯的通讯工具演化成一种新兴传播媒介时，一些“黄段子”、“灰段子”在中国内地人们指间大量传播。薄熙来明确提出，要用先进的文化占领手机短信阵地，使传播广泛的手机短信充分体现社会主义核心价值体系。<br />
<br />
二00八年六月以来，在薄熙来的倡导下，重庆开展了“唱红歌、读经典、讲故事”等活动，以激发重庆百姓的爱国热情、凝聚精神力量，达到推动当地经济社会发展的目的。当地官员称，“红言颂”是不失为以上活动在新媒介中的一种延续。<br />
<br />
国家工业和信息化部部长李毅中专程为该活动发来贺信，肯定该赛事可传承光荣历史、弘扬时代精神、传播文明风气，对推进以先进文化占领网络舆论阵地、抵制不良有害信息，构建和谐社会具有重要意义。 (本文来源：中国新闻网 作者：郭虹)<br />
