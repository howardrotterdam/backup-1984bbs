													<h2>薄书记的红文化一浪高过一浪</h2>
						
						
																											<div id="postmessage_84167" class="t_msgfont">● 张晓中 重庆特派员<br />
<br />
　　由中共政治局委员、重庆市委书记薄熙来力推的“红色文化”，一浪高过一浪。从开始唱红歌、读经典、讲故事，发展到今天的发“红言”短信。<br />
<br />
　　薄熙来前天带头发出了第一条“红言”短信。内容是：“我很喜欢毛主席的几句话：‘世界是我们的，做事要大家来’，‘世界上怕就怕认真二字，共产党就最讲认真’，‘人是需要有点精神的’，这些话很精干，很实在，也很提气。”<br />
<br />
　　所谓“红言”就是指歌颂中共革命传统的“红段子”或“红语录”。为了避免单纯提倡“红言”少人响应的尴尬，官方也鼓励把古今中外的名人名言、格言警句以及市民自创、符合中华传统美德、社会公德的励志箴言当作“红言”传播。<br />
<br />
　　重庆红色文化专家厉华昨天接受本报采访时表示，薄熙来推动红色文化其实是中共推动政治、思想建设的新尝试，现今的中国人急需一种精神支柱：“最现成的就是积淀了几千年中华文化精华的红色文化。”<br />
<br />
　　薄熙来前天在“红‘言’颂――2009年第二届红色短信创作传播大赛”启动仪式上说：“毛主席当年讲的这些话，曾经激励、教育了我们那一代人。今年是新中国60周年华诞，重温这些话，回想毛主席带领中国人民走过的艰辛岁月，令人感慨万千！”<br />
<br />
　　他说：“经过30年的改革开放，中国已成为举世瞩目的大国。回想从贫弱走向繁荣的曲折道路，我们更加认识到，无论在多么艰苦的条件下，都要有革命的理想和信念，这是一股巨大的精神力量！这也就是我们今天倡导的红‘言’。”<br />
<br />
　　官方选择短信作为传播“红色文化”的平台，是因为手机是目前中国使用率最高的通讯工具，尤其是年轻人，几乎人手一机。光重庆就有1300万手机用户，占重庆人口的三分之一强。手机已从单纯的通讯工具演化成一种新兴传播媒介，一些隐喻男女关系的“黄段子”和讽刺官场的“灰段子”在中国手机用户之间大量传播，引发官方的忧虑。<br />
<br />
　　薄熙来要求，“用先进的文化占领手机短信阵地，使传播广泛的手机短信充分体现社会主义核心价值体系”。<br />
<br />
　　在薄熙来的倡导下，重庆去年开展了“唱红歌、读经典、讲故事”等活动，以激发民众的爱国热情、凝聚精神力量，达到推动当地经济社会发展的目的。薄熙来还亲自带领官员一起唱颂扬中共的歌曲，为《读点经典》一书写序，邀请一些知名的“红色艺术家”到重庆讲故事。<br />
薄熙来在为《读点经典》序言中说：“有古往今来的诗词佳句，散文精粹，开本小巧，好读好带。虽然两小时就能看完，且每月只一小本，但久而久之，也会令你的知识结构更加完善，有如多种维生素片，虽每日一粒，亦可养身。”<br />
<br />
　　以歌颂中共“主旋律”而知名的相声演员姜昆也受邀到重庆讲故事。他说，重庆这次活动有传统基础和群众基础，肯定能做好，可以形成一个响当当的文化品牌，甚至可以带起全国的讲故事热潮。<br />
<br />
　　专门从事重庆红色文化研究的红岩联线主任厉华说，重庆作为一个以抗战出名的城市，具有得天独厚的红色文化积淀。红色文化已经成为重庆个性凸显的一个标志，薄熙来推动红色文化，就是要提升重庆人的精气神。<br />
<br />
　　他表示，重庆的历届市委书记都一直推动红色文化。“到了薄书记，因为他出身在红色家庭，成长在红色年代，由他来推动红色文化再自然不过。”薄熙来为原中共中央顾问委员会常务副主任薄一波之子。<br />
<br />
　　不过，对于薄熙来倡导的红色运动，中国网民有不同看法。一名网名为“虎胡寺庙”的网民认为，唱红歌是一种“愚民教育，形式主义”。他说：“人民一定要抵制，当父母一定不要忘记告诉子女什么才是是非对错，你们这代人已经被愚了60年，难道还要让下一代也被愚弄洗脑吗？”<br />
<br />
　　网民“小草”则表示，唱红歌有利有弊，不喜欢的人不去唱就是了，但是也有很多喜欢唱红歌的人。<br />
<br />
　　另有北京学者指出，改革开放以来，官方多次发动以弘扬“红色文化”为主题的宣传活动，但往往都是虎头蛇尾，难成气候。</div>

							
							
							
							
