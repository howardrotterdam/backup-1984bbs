													<h2>高耀洁：在火山口上呐喊</h2>
						
						
																											<div id="postmessage_800123" class="t_msgfont">高耀洁：在火山口上呐喊//是@篇幔04年的。。。<br />
<br />
<br />
南方网精彩专题：抗击艾滋 动人的画卷<br />
<br />
　　高耀洁的光芒足以照亮我们人性中的阴暗与潮湿。――网友：巴柄<br />
<br />
&nbsp; &nbsp; 这些事情，本不应由这么一位老人做。――网友：糊涂<br />
<br />
&nbsp; &nbsp; 要把艾滋病拒之于国门之外，这个想法非常天真。在我们的许多地区，已经有许多人死于艾滋病，并留下许多孤儿。我们还是晚了。我们可能要遭受更大的痛苦才知道我们所面临的问题的严重性。――高耀洁<br />
<br />
　　2003年，中国政府宣布艾滋病毒感染者人数为84万，发病人数为8万，中国的艾滋病患者人数已跃居亚洲第二，仅次于印度。<br />
<br />
　　2003年9月，卫生部常务副部长高强在出席联合国艾滋病问题特别会议时承认，“艾滋病防治工作仍面临着严峻的形势，艾滋病在中国还没有得到有效遏制。”<br />
<br />
　　中国严峻的艾滋病疫情，曾被国际社会普遍担忧为“坐在即将爆发的火山口上”。<br />
<br />
　　高耀洁，是坐在火山口上呐喊的人。2003年，是她呐喊的第7年。<br />
<br />
　　“知识是艾滋病的最佳疫苗”<br />
<br />
　　12月25日，小伉伉的父母来到高耀洁家，看到高医生墙上挂着的伉伉照片，夫妻俩又嚎啕大哭起来。<br />
<br />
　　4岁的伉伉，3年前因输血感染艾滋病，于2003年10月8日死亡。<br />
<br />
　　伉伉的父亲说，给伉伉输血的乡村医生打电话找血头，血头用三轮车拉来一个血型相同的人，当场给伉伉输血，没做任何检测艾滋病的手续。<br />
<br />
　　自从政府打击非法采血后，血头从公开转入地下，改成和医生联系卖血。今年4月20日，伉伉的父亲去找医生打官司时，他还在用类似办法给人输血。<br />
<br />
　　高耀洁送给伉伉的父亲2000册防艾手册去当地农村发放。这是他第三次来取小册子。<br />
<br />
　　“高医生77岁还搞宣传，30岁的我为什么不去宣传？还是那句话，只要有口饭吃，就宣传下去。”伉伉的父亲对自己当年的举动悔恨不已，那次直接输血比购买血库的血便宜很多，他还专门请血头和输血的感染者喝了酒。<br />
<br />
　　更让高耀洁感到可恨的是一个有4个女儿的农民，老婆和一个女儿已确诊感染了艾滋病，但为了生个儿子，他竟然又抱着侥幸心理要孩子。<br />
<br />
　　结果生下来的儿子被证实又感染上了艾滋病毒。<br />
<br />
　　“知识是艾滋病的最佳疫苗。”高耀洁说。<br />
<br />
　　她将世界卫生大会的“世界健康与人权奖”和亚洲“拉蒙・麦格塞公共服务奖”的奖金，都用在了印制防艾宣传品上。从1996年自费编印第一批防艾资料至今，宣传册已经印了17期，总印数达到67万份。<br />
<br />
　　“惟一的办法，只有预防，必须把‘防艾’知识交给群众，让广大群众拿起‘防艾’武器，才能控制艾滋病的流行与蔓延。”<br />
<br />
　　全国各地都有人来信索取防治艾滋病的书籍和资料。在她收到的近万封来信中，70％以上是此类信件。<br />
<br />
　　她还把书委托有关机构发放，去年8月的一次发放记录显示：省卫生防疫站21000本，省教育厅关心下一代工作委员会3150本，省图书馆5075本。她最大的担心是这些书被堆在仓库里或被当废品卖掉。<br />
<br />
　　高耀洁是第一个广泛宣传“在中国，血液是艾滋病最大的传播途径”的人。<br />
<br />
　　一位权威的艾滋病流行行为专家称：中国的艾滋病患者和病毒感染者63.9％以上是通过吸毒（共用针头）造成的，经采血（血浆）途径感染人数占9.7％。还有某专家在发言中再三强调，中国的艾滋病70％~90％是通过色情行业传播的。<br />
<br />
　　高耀洁不赞同这种观点。<br />
<br />
　　“据近万封群众来信尤其是乡村医生的反映显示，国内艾滋病绝大多数都是血传播。少数是母婴传播，而且在农村呈局灶性发病。在当年血浆经济越火爆的村庄，艾滋病发病率越高。”<br />
<br />
　　近千封“骗子信”<br />
<br />
　　“我和骗子进行较量是从上个世纪80年代开始的，现在我实在太疲惫了。我不想再让媒体报道我，引来更多的骗子利用我的声望来骗艾滋病病人。”<br />
<br />
　　用高耀洁的话说，她收到的近万封群众来信中，有1／10是“骗子信”。她辨认骗子信的标准有三：（1）自制药品（没有国家药字批号）专治艾滋病者；（2）自称艾滋病最好治，自制的药物有100％疗效，治好了若干艾滋病病人者；（3）祖传秘方专治艾滋病，其中疗效得到了名人的肯定、签名、题词甚至获奖等。<br />
<br />
　　以上三项，符合任一项的信件，高耀洁均不予理睬。<br />
<br />
　　但写信的人得不到答复就干脆直奔她家，找她合作研制治疗艾滋病的中药。坐下寒暄几句便试投敲门砖――“事成后如何分红”等。还有的想请她当艾滋病专科医院院长或名誉院长的。<br />
<br />
　　文楼村艾滋病患者程某告诉高耀洁，有不少人打着“送药”的旗号，将一些粗制滥造的伪药、劣药吹嘘为“神药”。有的拿着“药粉”让病人用鼻子吸，说是新发明的“气味学”、“气味疗法”，用鼻子一闻就好了。该村不少艾滋病患者深受其害。<br />
<br />
　　睢县的艾滋病病毒携带者刘某在电话中告诉高耀洁：“有人告诉我北京有一种机器，艾滋病病人坐上摇几次病就好了……”<br />
<br />
　　高耀洁提起这些就胸口发闷，她心脏不好，但她还是忍不住一次次替艾滋病人悲伤――<br />
<br />
&nbsp; &nbsp; “我救助一个艾滋病人几千块，不到一周就被骗子们的‘神药’骗走了。”<br />
<br />
　　艾滋孤儿危机<br />
<br />
&nbsp; &nbsp; “有人问现在‘防艾’工作的当务之急是什么，关键是孤儿，中心是孤儿，孤儿第一，其他问题次之！否则，会埋下民族的灾难。”<br />
<br />
　　在河南的上蔡、新蔡、柘城、睢县等艾滋病感染者较多的地方，有些艾滋病感染者或他们的亲属开办了“孤儿院”、“关爱之家”等，高耀洁对之保持着足够的距离及警惕。<br />
<br />
　　在她看来，这些人有着利用孤儿名义为自己赚钱的嫌疑――“他们家里都盖起了小楼，来找我时，手里提着笔记本电脑……钱没花到孩子身上，孩子该受罪不是还受罪吗？”<br />
<br />
　　高耀洁也不赞同采取孤儿院模式救助艾滋孤儿。<br />
<br />
　　“孩子过早地遭遇了不幸，他们有相同的经历，聚集在一起，容易产生不正常的心理，结拜义兄弟弊端更大。集中办孤儿院还牵扯到经济问题，如租房、工资、生活用品等等，加重了社会的负担。”<br />
<br />
　　国际上也并不推崇孤儿院这种形式，因为孤儿院会令儿童被社会看成另外一个群体。美国现在有一种方式：找两个人分别担任父母角色，抚养一个孩子，孩子生活上得到的关怀跟正常人一样，因此他的人格是健全的。<br />
<br />
　　高耀洁能想出“分散抚养”模式，完全出自一颗母亲的心。“孩子们有个新家就好了。”她一直关心的6个孤儿在她的努力下，被山东曹县的农户们收养了。<br />
<br />
　　呐喊的回声<br />
<br />
&nbsp; &nbsp; “真正的英雄是那些在自己的国家不让（艾滋）这个问题溜走的人。”2003年11月10日，来清华大学参加“AIDS与SARS国际研讨会”的克林顿这样说。<br />
<br />
　　高耀洁7年来执拗地揪住这个问题不放，到今天几乎是心力交瘁。<br />
<br />
　　她甚至深深地被所救助之人伤害。2001年6月13日，疫区某村派人来找她为艾滋病病人要药，她交给他一箱药品，这箱药品未曾到村，便被来取药的人卖了换钱。<br />
<br />
　　近两年来，她多次为多个疫区的病人送衣物，每次经手人都说，已把衣物发送给了许多村民。2002年12月23日，她突然访问某县某村的经手人家里，发现她房间里大堆衣物。<br />
<br />
　　而络绎不绝的骗子又使出了新招。<br />
<br />
　　有一个人说是某中医院的，要为艾滋孤儿捐款。等高耀洁回信讲述孤儿情况时，他就原形毕露，索要病人地址，说自己有自配药，能100％地治好艾滋病。<br />
<br />
　　去年还怕影响住院率，大家都不愿意作艾滋病指定医院，今年“艾滋病研究会”之类组织却像雨后春笋一般冒了出来。<br />
<br />
　　2003年11月上旬的一天，高耀洁从报纸上看到新华社消息：“我国作出五大承诺，免费治疗贫困艾滋病患者。”她流泪了。<br />
<br />
　　12月18日，吴仪副总理到达驻马店市著名的艾滋病村文楼村。她先去了村里的卫生所视察，后来还走访了两户人家。<br />
<br />
　　吴仪告诉同行的河南省领导，要严明疫情报告纪律，加强疫情监测，对瞒报、漏报和迟报要追究责任。<br />
<br />
　　吴仪研究艾滋病防治措施时出人意料地请来了高耀洁。<br />
<br />
　　高耀洁没有提自己7年的艰辛，她谈的是依靠她个人的力量难以解决的事。艾滋孤儿的生存问题、教育问题、心理问题急待重视。她讲了1960年政府出面使3000名上海孤儿进大草原成功被分散抚养的故事。第二个问题是打击艾滋病假药、治理假广告。第三个问题是廓清对中国艾滋病传播途径的认识，消除歧视，最大范围地救助艾滋病人。<br />
<br />
　　对于艾滋病，在2003年或许被更多普通民众记住的，是国务院总理温家宝与艾滋病人握手的画面。<br />
<br />
　　温家宝看似很随意的举动，被赋予了更多的意味――他实际上在代表政府向外界传递一种明确而强烈的信息――中国政府重视艾滋病现状，并有决心最大可能地走出抑制艾滋病蔓延的一大步。高耀洁在火山口上7年的呐喊在2003年听到了清晰的回声。（编辑：李琳）<br />
