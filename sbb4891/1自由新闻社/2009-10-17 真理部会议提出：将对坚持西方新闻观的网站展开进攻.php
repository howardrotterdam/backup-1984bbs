													<h2>真理部会议提出：将对坚持西方新闻观的网站展开进攻</h2>
						
						
																											<div id="postmessage_286061" class="t_msgfont">独家：宣传内部会议提出对付海内外中文网的新举措<br />
2009年10月16日 <br />
<br />
北京报道，在昨天举行的宣传内部研讨会上，一位宣传部领导透露了“网络管理三年计划”的部分内容，这份计划分为两部分，一部分是对大陆的网络管理纲要，是由宣传部、国新办和公安部共同执行，由宣传部负责牵头。除了继续实行国庆节前颁布的禁止网民互动内容（论坛、博客）上各大网站主页之外，要求国新办组织专家学者，厘定出大网站首页内容的建议。<br />
<br />
在各大网站逐渐减少网民互动内容的同时，国新办将对网站上首页的内容作出进一步的规范，如何向纸媒靠拢，但又照顾到网络语言和形式的特殊性，成为主要的研究工作。领导说，要在最短的时间，完全彻底地掌握网络舆论导向。 <br />
<br />
宣传部也不讳言，从过去三年的情况看，网络已经成为反腐败的主力军。对过去三年落马的贪官的统计显示，高达87%的官员落马是因为网民的“爆料”和揭发，而这些网民的言论虽然都是通过博客和论坛，却得到各大网站的力推和暗中支持。一份国新办提供的研究资料显示，没有被网站关注而加以推荐的“爆料”与揭发内容，一般都不会引出什么结果。<br />
<br />
宣传部要求各部门继续利用网络反腐，要求“相关单位”能够从海量的网络信息中寻找有利反腐的材料。但却拒绝让这些材料被更多网民看到，所以才会同时大幅收紧“网民”在网络上的表达空间。这种一边要网络反腐，一边却又收紧网民的言论空间的做法，让博讯记者困扰不已。<br />
<br />
这位前来这个内部研讨会做主题发言宣传部官员提醒与会人员，在各大网站被收归到舆论控制的正确轨道上的同时，大家要注意一些独立网站的动向，包括一些独立博客，因为他们的作用将会相应增加。他希望各部们要对那些影响大的独立网站和博客加强监控和管理，他说，“不要害怕关网站，我们国庆节前要出台一些收紧措施，有同志说，这样可能会像绿坝一样引起反弹。现在大家看看，没有什么，这个国家还是共产党的，舆论导向还是要牢牢控制在我们手里，腐败不会弄垮我们，失去舆论导向则会亡党亡国。”<br />
<br />
这份三年计划中，有三分之一的内容是针对海外中文网站的，针对这部分，除了加强屏蔽让他们逐渐失去国内中文读者而无法生存外，还要把重点放在外宣、外事和安全机构的共同努力上。他说，大规模收购海外网站是行之有效的，但这个方法并不可取，因为海外华人是唯利是图的，当他们发现我们在像收购多维等网站那样收购一些海外中文网站时，就会趁机弄出更多网站，那到时就是再拨款几百个亿，可能也买不过来。<br />
<br />
他说还是要使用更灵活的手段。如果一些海外网站能够积极配合我们，有意悔改，可以设立一个期限，限期他们整改，删除不健康的内容，设立一些敏感过滤词语，封一批IP，不排除在他们能够自律的情况下开放一批国外网站登陆（意思是不被屏蔽），这样也会对所有海外中文网站起到示范作用。这位官员说，据他所知，这对很多海外中文网站是有吸引力的。<br />
<br />
<strong>但他同时强调，对于那些顽固的“反党反华”的网站，坚持所谓西方新闻观不知道回头的中文网站，要毫不留情地使用一切办法让他们无法生存下去。虽然这位官员并没有进一步解释什么叫“一切办法”，但毫无疑问，使用黑客破坏网站，加强屏蔽以及针对网站工作人员的迫害升级，都是他们一直在使用的手段。</strong><br />
<br />
博讯记者北京报道。</div>

							
							
							
							
