													<h2>传北京研究对中文网展开更严格监管</h2>
						
						
																											<div id="postmessage_286567" class="t_msgfont">传北京研究对中文网展开更严格监管&nbsp;&nbsp;<br />
20091017 11:21:56&nbsp; &nbsp;&nbsp; &nbsp;<br />
 <br />
（中央社台北17日电）据传中国有关部门正进一步加强对因特网的监管。海外博讯网表示，在中国宣传部门内部研讨会上提出「网络管理3年计画」，包括对内严控网站，对外加强防堵中文网站。<br />
<br />
报导表示，官方除继续实施禁止网友交互内外，也要求召集学者专家制定大网站首页内容的建议。「网络管理三年计画」要将管理网络的程度向平面媒体看齐，「但又要兼顾到网络语言和形式的特殊性等。」<br />
<br />
北京1所大学网站职员向「自由亚洲电台」表示，当局对他们的监管比以前严，现在都不敢随便乱发言论，「我们学校的宣传部在负责，出来的东西（文字）都是要主流的」。<br />
<br />
据称这项计画有1/3是针对海外中文网，除了加强防堵外，大规模收购海外中文网「是行之有效的，但不鼓励，因为花费太大，而且收购本身会引发出更多网站，收不过来。」<br />
<br />
北京1位传媒负责人披露，上级宣传部门为了避免受到舆论批评，现在多采取口头通知的方式，很少下文件，以免文件被有心人上传到网站。<br />
<br />
博讯网也提到，中国宣传官员还要求要打击海外「反党反华」的网站，「要毫不留情地使用一切办法，让他们无法生存。」<br />
<br />
据了解，一些经常刊登批评中国政府文章的海外网站，常遭到来自中国黑客攻击，包括中国维权和异议人士的网站。981017</div>

							
							
							
							
