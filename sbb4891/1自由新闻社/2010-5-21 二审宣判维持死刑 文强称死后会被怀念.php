10、从袁老大最后的哀号“我不服，我要检举！”看来，做掉汪兴也很可能是背后有人指使，然后达成某种交易。但不幸的是，老美的CIA也不是吃干饭的，大家应该可以记起去年炒的沸沸扬扬的邻邦伪钞、贩毒和洗钱的事吧？友邦震怒了，咱们还没到摊牌的时候。 <br />
