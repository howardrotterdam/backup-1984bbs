袁妻透露有人要灭口 <br />
<br />
“辽宁某种势力想要袁宝Z的命！”袁宝Z妻子卓玛去年5月间曾对媒体如此说。据透露，2002年袁宝Z母亲下葬，那位高官（即据传袁宝Z举报的要员）亲自接风，并安排武装保镖，驾驶武警牌照轿车跟随袁宝Z。此后不久，该高官就提出由袁宝Z的建昊集团为他推荐的某公司到香港上市提供支持，袁宝Z随即出资亿元支持该公司上市。该笔资金袁宝Z后来曾经多次催收，至今尚未收回。 <br />
<br />
2005年6月1日，袁宝Z在看守所向两位代理律师口述了这位高官通过汪兴（恰恰就死者）找到他，要求帮这位领导洗“黑钱”，并开出每洗1000万元提成300万元的事情，当律师通过看<br />
守所干警让袁宝Z在会见笔录上签字时，看守所找出各种理由就是不让袁宝Z签字。无奈之下，卓玛只好在北京长安公证处的公证下，将这些举报材料寄送至辽宁省有关领导和部门。 <br />
<br />
自从经历了这些事情之后，卓玛倒显得坦然了。她说是祸躲不过，如果她因为这些事被害，反倒证明了自己先生的清白。因此，她毅然搬回到了自己的家。卓玛透露，此前和此后自己和家人、律师曾经多次遭遇人身威胁甚至袭击。 <br />
<br />
第一件事发生在2004年的九、十月份。按照卓玛的描述，当时在她家附近的一条胡同里，一辆貌似依维柯的汽车以很快的速度撞向卓玛，由于当时卓玛的哥哥在场，所以逃过一劫。自此之后，卓玛离开了原先的住地。 <br />
<br />
第二件事发生在卓玛搬家之后。据卓玛介绍，自从胡同历险结束后，为了孩子以及自身的安全，卓玛把家搬到了一个非常隐蔽的别墅区，但是没过多久，就先后有一辆奥迪和一辆小面包车找上门，其理由分别是找卓玛和修水管。事情发生后，卓玛决定把孩子送往外地。 <br />
<br />
第三件事更为惊险。2004年8月17日晚上，卓玛与袁宝Z的法律顾问一行，乘最晚一班飞机到达沈阳机场。下飞机之后，卓玛就发现有人手中拿着自己的照片，然后从沈阳到辽阳的一路上都被人跟踪。“我很紧张，律师也很紧张。我们上了车以后那个车就跟踪我们，我们就躲，从沈阳到辽阳的路上有很多大货车，我们的车就躲在两个大货车中间，车速一直保持在30公里。” <br />
<br />
疑点重重的袁氏三兄弟案件 <br />
<br />
袁宝Z雇凶杀人案引起的轰动并没有随着袁氏三兄弟被执行死刑而完结，在大陆网坛上更是掀起了众多网民的怀疑和评论，并要求揭开事实真相。 <br />
<br />
一名网友在大陆某知名论坛里写道：“袁氏三兄弟留给世人最多的是猜测有黑幕，更多的是对比：贵州按顺的强奸杀人案居然没有一个被判死刑。说明我国的法律弹性如此之大。天理难容直接参与杀人的安顺强奸犯没判死刑，而间接参与的袁富豪却被判死刑。说明什么？”有网民表示：汪兴与某XXXXX高官也有联系，汪和袁兄弟们都死了，知道秘密的人或许就少了。 <br />
<br />
更有网友大呼冤案，“此案有多个疑点1.为何被告如此之快就被处决，有杀人灭口之嫌2.被告人已有如此地位，为何要叫自己亲兄弟去杀人，为何不利用黑道摆平3.即使自己兄弟杀人也不会笨到将自己亲兄弟供出来他还不如一人扛死留下兄弟照顾家人.4为何被告提出上诉.而不准其上诉.5.为何人民群众没有听到被告一句申辩就被处决6.君不见街上流氓犯罪杀人等，又有几个被绳之以法了的。为什么如此有地位如此有能力之人，轻易被拿下…” <br />
<br />
有网民表示，失一命者杀三人，也许只有在中国的法律下才能干的出来，联想到曾经的刘涌案，为什么东北的法律就可以那样的随便？北京那帮老不死的学究们，为什么肯为了一个恶霸不惜和全国人民作对，而在这样一个疑点重重的案件面前却集体销声匿迹，放弃了这个扬名立腕兼发大财的好机会？ <br />
<br />
网民质问，“最高院为什么把这样一个从法理和事实上都说不过去的案件交给辽宁高院复审？最高院到底有没有认真执行死刑覆核程序？到底什么人有这样大的权力，能让中央高层为之忌惮三分？”并且叹道， “一个手眼通天的富翁都可以死的这样不明不白。中国的普通百姓又该怎么样保障自己的权利？” <br />
<br />
是谁要治袁宝Z于死地 <br />
<br />
坊间流传一种说法，辽宁省众多高官都接受过袁宝Z的巨额贿赂。另外还有些高官以拆借资金为名，向袁宝Z索贿巨额资金，少者几十万，多者上亿，其中牵扯北京与辽宁省的高官多达百人。出乎袁宝Z意料的是，这些拿他钱财的高官并没有替他消灾，反而却都欲置他于死地而后快。直到袁宝Z死的时候，才明白人死账烂的道理！ <br />
<br />
据之前描述，袁宝Z妻子卓玛去年5月间曾对媒体如此说“辽宁某种势力想要袁宝Z的命！”。并且称，该高官就提出由袁宝Z的建昊集团为他推荐的某公司到香港上市提供支持，袁宝Z随即出资亿元支持该公司上市。该笔资金袁宝Z后来曾经多次催收，至今尚未收回。<br />
<br />
此周刊指称，袁宝Z曾将一亿二千万元借给某常委高官，利用该笔款项参与内地一家公司到港上市的活动，至今欠款仍未归还。 <br />
<br />
但事情好像远没有完，据内地媒体透露，2005年6月1日，袁宝Z在看守所向两位代理律师口述了这位高官通过汪兴（恰恰就死者）找到他，要求帮这位领导洗“黑钱”，并开出每洗1000万元提成300万元的事情，当律师通过看守所干警让袁宝Z在会见笔录上签字时，看守所找出各种理由就是不让袁宝Z签字。而去年10月，袁宝Z死刑被缓期执行后，袁宝Z的律师刘家众也透露说，袁宝Z案峰回路转，源于他举报了一起涉及1.2亿港币的经济犯罪大案。据袁宝Z举报这位高官甚至掌握着该省毒品犯罪和假钞买卖活动。 <br />
<br />
据网民在论坛上贴的资料显示，省常委及政法委在港上市的公司名称是，锦恒汽车安全技术控股有限公司，办公地址位于香港干诺道中55号鳄鱼恤大厦二期12楼1203室。主要业务为于中国生产及销售汽车安全产品。XXX本人是公司的主席兼执行董事。 <br />
<br />
同时由于此公司的名称与前***总书记***的儿子的名字（绵恒）看起来很相近，被网民质疑是否与之有关系。 <br />
<br />
而下面一网友的帖子更是让人感到其中所暗藏的黑幕远远超出这个雇凶杀人案件的本身, 而事实的真相到底是什麽，其内幕还会隐藏多久，现在还无人可知。 <br />
<br />
现实中上演的大片 <br />
<br />
1、袁老大杀人可能是迫不得已，并不是所公布的原因。 <br />
<br />
2、被袁老大所干掉的汪兴据说横趟黑白两道，应该是知道了一些袁老大不可告人之事。 <br />
<br />
3、什么不可告人之事必须灭口呢？线索如下：油田，香港上市公司，伪钞，贩毒。 <br />
<br />
4、袁老大及其兄弟为什么会被全部灭口呢？因为抖落出几百亿的油田向某些人暗示什么东西。这些钱可以判定肯定不是他自己的，而是替别人运作的。这又是另一条线索：洗钱。 <br />
<br />
5、谁人会把伪钞和贩毒的钱用油田和香港上市公司洗干净呢？而且还部分通过一个边境省份的政法委书记？答案只有一个，这个边境省份过去的那个国家的KING氏家族（编者注：估计应该是北韩）。 <br />
<br />
6、KING氏家族为什么要把不正当的钱洗干净？答案也只有一个：留后路。 <br />
<br />
7、大陆为什么肯帮这个家族干这些不可告人之事？答案也只有一个：政治交易（大致应该是制约美日，以前明给，现在因为民众意识的觉醒改暗补了）。 <br />
<br />
8、至于所谓的事情起因，袁老大让汪兴教训刘汉，恰恰是最不重要也最无关紧要的事，是为了自圆其说而特意挑选出来的。在大陆生意场上混，凡是白手起家 的，谁没有打打杀杀的经历？即使没有，发了财以后也会用江湖手段教训一些讨厌闹心不顺眼的家伙。这些事太司空见惯，即使袁老大都没有，随便找件搁他头上别 人也不会觉得奇怪的。而且，汪兴不是已经死了吗？死人是不会开口讲话的。 <br />
