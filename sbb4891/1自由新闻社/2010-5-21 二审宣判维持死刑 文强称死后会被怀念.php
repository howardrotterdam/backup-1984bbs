11、某网站所有的评论都是同情袁老大和要求揭盖子的，这不排除袁老大的残余势力在操纵，也可能是该网站的老板兔死狐悲，更可能是有更大的黑手介入。大家知道，该网站是有美资背景的，而且据传说还和日本有什么关系。这不，这条新闻及评论都蒸发了。 <br />
