<br />
　　昨日审判席上,文强一直静静地听着,情绪一直很稳定。直至听到法官宣读到“死刑立即执行”的判决时,他依然是出人意料的平静。“表面上看起来很平静,但他心里很无奈。”文强的二审辩护律师宣东说,他一直在观察文强的反应。宣东说,前日与文强会谈时,文强已预感到二审可能维持原判,心理上应该有所准备。<br />
