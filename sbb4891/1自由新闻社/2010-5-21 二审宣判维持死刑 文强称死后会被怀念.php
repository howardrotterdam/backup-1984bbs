<br />
　　本报重庆讯 昨日上午,重庆市高级人民法院对文强案二审公开宣判,依法驳回文强、周晓亚、黄代强、赵利明、陈涛的上诉,维持一审判决。至此,从2008年7月10日开始的轰轰烈烈的重庆打黑风暴画上了一个句号。<br />
<br />
　　重庆高院经二审审理确认,1996年至2009年期间,文强利用其先后担任重庆市公安局党委委员、党委副书记、副局长,重庆市司法局党委书记、局长的职务便利,为他人职务晋升、工作调动、就业安置、承揽工程等谋取利益,先后多次单独或伙同其妻周晓亚收受包括黑社会性质组织的组织者、领导者在内的他人财物,折合人民币共计1211万余元。其中,文强、周晓亚共同收受他人财物折合人民币 449万余元。<br />
<br />
　　2003年~2008年期间,文强明知王天伦、谢才萍、岳宁、马当、王小军等人组织、领导的多个黑社会性质组织从事有组织的违法犯罪活动,仍予以包庇、纵容。2007年8月28日晚,文强还强行与某女子发生了性关系。此外,文强对1044万余元财物不能说明来源。<br />
<br />
　　重庆高院认为,原判决认定文强犯受贿罪,包庇、纵容黑社会性质组织罪,强奸罪,巨额财产来源不明罪,事实清楚,证据确凿充分,定罪准确,量刑适当,审判程序合法。文强所犯受贿罪,数额特别巨大,情节特别恶劣,后果特别严重。据此,重庆高院裁定:驳回文强上诉,维持原判;根据《刑事诉讼法》的相关规定,对维持原判决判处被告人文强死刑、剥夺政治权利终身、并处没收个人全部财产的刑事裁定,报请最高人民法院核准。重庆高院同时驳回了周晓亚、黄代强、赵利明、陈涛的上诉,维持原判。<br />
<br />
　　据了解,文强案二审判处死刑立即执行后,将提交最高人民法院进行复核。如果最高人民法院复核通过,则会被立即执行,如果被最高人民法院驳回则转为死刑缓期执行。<br />
