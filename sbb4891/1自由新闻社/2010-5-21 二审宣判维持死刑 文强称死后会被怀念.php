宣判现场 市高院供图 华龙网发<br />
<br />
<img src="http://img1.gtimg.com/cq/pics/hv1/214/76/528/34352794.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
文强在今日的宣判现场 市高院供图 华龙网发<br />
<br />
华龙网5月21日11时39分讯（数字记者李心成、何青枚）5月21日上午，重庆市高级人民法院对文强案二审公开宣判，依法驳回文强、周晓亚、黄代强、赵利明、陈涛的上诉，维持一审法院的判决。<br />
<br />
4月14日下午，重庆市第五中级人民法院对文强案进行一审公开宣判，以受贿罪，包庇、纵容黑社会性质组织罪，巨额财产来源不明罪，强奸罪数罪并罚判处文强死刑，剥夺政治权利终身，并处没收个人全部财产。其余四名同案被告人分别被判处八年至二十年不等的有期徒刑。<br />
<br />
一审宣判后，文强等五人不服，向重庆市高级人民法院提出上诉。重庆高院依法组成合议庭，依照刑事诉讼法的规定，对该案进行开庭审理。<br />
<br />
重庆高院经二审审理确认，1996年至2009年期间，文强利用其先后担任重庆市公安局党委委员、党委副书记、副局长，重庆市司法局党委书记、局长的职务便利，为他人职务晋升、工作调动、就业安置、承揽工程等谋取利益，先后多次单独或伙同其妻周晓亚收受包括黑社会性质组织的组织者、领导者在内的他人财物，折合人民币共计 1211万余元，其中，文强、周晓亚共同收受他人财物折合人民币449万余元。2003年至2008年期间，文强明知王天伦、谢才萍、岳宁、马当、王小军等人组织、领导的多个黑社会性质组织从事有组织的违法犯罪活动，仍予以包庇、纵容。2007年8月28日晚，文强还强行与某女发生了性关系。此外，文强对 1044万余元财物不能说明来源。<br />
<br />
重庆高院认为，原判决认定文强犯受贿罪，包庇、纵容黑社会性质组织罪，强奸罪，巨额财产来源不明罪，事实清楚，证据确实充分，定罪准确，量刑适当，审判程序合法。文强所犯受贿罪，数额特别巨大，情节特别恶劣，后果特别严重。据此，重庆高院裁定：驳回文强上诉，维持原判；根据《刑事诉讼法》的相关规定，对维持原判决判处被告人文强死刑、剥夺政治权利终身、并处没收个人全部财产的刑事裁定，报请最高人民法院核准。<br />
<br />
重庆高院同时驳回了周晓亚、黄代强、赵利明、陈涛的上诉，维持原判。<br />
<br />
快讯：重庆市民闻讯文强二审维持原判 放鞭炮庆贺<br />
<br />
华龙网5月21日11时48分讯（数字记者李心成、何青枚）今日上午11时39分，重庆市高级人民法院对文强案二审公开宣判，依法驳回文强、周晓亚、黄代强、赵利明、陈涛的上诉，维持一审法院的判决。文强被判处死刑、剥夺政治权利终身、并处没收个人全部财产。<br />
<br />
宣判结果宣布不久，市高院外响起隆隆的鞭炮声。30多名普通百姓拉起“判处文强死刑，法律万岁”的横幅，并放起鞭炮以示庆祝。<br />
<br />
快讯：审判长宣读裁定书耗时99分钟<br />
<br />
华龙网5月21日11时50分讯（数字记者李心成、何青枚）<br />
今日上午10时，重庆市司法局原局长文强等人涉黑案在重庆市高级人民法院二审宣判。审判长宣读刑事裁定书，持续99分钟。文强等人全程表情平静，神色沮丧。<br />
