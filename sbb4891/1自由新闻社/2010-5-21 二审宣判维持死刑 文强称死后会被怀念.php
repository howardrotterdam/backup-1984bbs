<br />
　　宣东说:“现在好像文强成了中国最大的贪官,这有舆论放大的原因。”考虑到支持重庆打黑,考虑到舆论对主审法官的影响,他也理解文强被判处死刑。但他希望最高人民法院能作出公正的判决,排除舆论的干扰。<br />
<br />
