																											<div id="postmessage_264155" class="t_msgfont">盛世大牛：财政部将发行50年期国债&nbsp;&nbsp;<br />
时间：2009/09/28 出处：第一财经 <br />
近期在广西南宁召开的四季度国债筹资会上传出消息，财政部计划四季度推出50年期国债。这将是有史以来最长期限的国债品种。<br />
<br />
一位国有银行人士认为，这是一个创新，估计初期发行两三百亿元“试探一下”。<br />
<br />
“因为市场需求不是太大，估计也是尝试而已，具体金额不会太大。”一位寿险公司固定收益部人士对CBN表示，寿险公司和社保基金对超长期投资品种有一定需求，大型国有银行也会“意思”一下。<br />
<br />
他认为这个产品的推出对深化债券市场建设、丰富投资品种、完善收益率曲线具有重要意义。<br />
<br />
国债市场的特点是中期品种占绝大多数，长期和短期品种较少，不利于各种资金需求，也使各期限市场定价基准有所欠缺。50年期超长期国债的推出无疑是债券市场基础设施建设的一部分。<br />
<br />
上述寿险公司人士认为，目前市场资金比较充裕，市场上交易的30年期国债收益率是4.1%，虽然很难确定50年后经济形势的发展，但利率不会太高，估计不会超过5%。<br />
<br />
也有市场人士认为，虽然50年期国债的发行有利于债券收益率曲线的完善，但50年期品种很难定价，因为目前市场上30年期品种较少。<br />
<br />
在上述会议上，财政部决定，四季度发行50年期和30年期国债各一期，原计划的20年期国债被取消发行。 (本文来源：第一财经日报 作者：冉学东)</div>

							
							
							
							
