2009 年2月，上海警方向媒体通报，成功破获了全国首例网上制作传播淫秽音频电子信息案――“动听中国”网站特大网上制作传播淫秽物品案，截止到2月27日，主犯龚某、张某、张某、马某 （女） 4人先后落网，警方依法扣押了一批涉案电脑， 3月16日四人被检察机关依法批准逮捕。<br />
<br />
上海警方在工作中发现 “动听中国” 网站，专门开设 “动听原创―夜话”栏目，以收费分成的方式，组织网民提供涉嫌淫秽的音频资料，并通过收费会员制传播淫秽电子信息。该网站在全球网站排名为54000名，在互联网音频资料类网站中，排名为全球第一。注册会员数已达50余万，其中网站收费会员数为3万余人、手机充值会员数达2万余人，收听用户遍布北京、上海、广东、浙江等全国各大省市乃至境外地区，已在互联网上形成了较大影响。<br />
<br />
据疑犯交代， 2008年初，总经理龚某授意技术部门负责人张某开设 “夜话”栏目专门提供淫秽类音频文件。网站采取采编网民自行上传的淫秽音频文件 （提供收费分成）和自行招募播音人员等方式，制作传播淫秽音频的电子信息。<br />
<br />
除网上搜集的淫秽电子信息外，公司以每小时40元的价格，组织招募播音小姐，先后录制了 《金麟岂是池中物》、 《少妇白洁》、 《天地之间》、《高树三姐妹》等一批连载的淫秽音频电子信息。这个由上海一家文化公司经营的淫亵网站，也以拆账方式，采用网民上载含性爱、叫床声等内容的录音资料，并通过收费会员制在网上传播淫秽音频档案。<br />
