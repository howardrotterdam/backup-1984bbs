																											<div id="postmessage_496948" class="t_msgfont">上海国保警察沈国良用车撞冯正虎<br />
<br />
&nbsp; &nbsp;2010年2月28日上午郭泉的辩护律师郭莲辉打来电话，他路过上海，想来看望我。我欢迎他光临寒舍，就放弃原定与妻子一起去岳母家过元宵节的计划，在家等候远道而来的客人。其间，一位上海的古玉鉴定专家沈纯理先生也来拜访我。<br />
<br />
&nbsp; &nbsp;中午12：00左右，郭莲辉律师由二位上海市民陪同，其中一位顾国平，我不是很熟悉的，另一位我根本不认识，但随同郭律师登门拜访我家的人都是我的客人。我们聊了一个半小时，就起身去我家附近的餐馆吃午饭。<br />
<br />
&nbsp; &nbsp;13：30左右，我们走到我小区的大门口，被一大批社区保安人员围住，其中有一位便衣警察要求郭律师跟他们去五角场派出所，郭律师当场拒绝，要求有合法的证件才可以跟他们走。我对他们说：这是我的客人，我们要去吃饭了，有什么事等我们吃饭后也可以谈，是谁叫他们去？这位便衣说：与你没有关系，是沈国良找他们。我说：他们是来我家做客的，你们又没有合法证件，你们要征求郭律师自己是否愿意配合。<br />
<br />
&nbsp; &nbsp;后来，郭律师与二位市民被便衣警察请上小车子送到五角场派出所，我也不清楚为什么这么急匆匆传唤郭律师，我的感觉他没有犯法，他刚到上海，已购了飞机票，明天回江西，只不过路过上海看望一下朋友而已。但愿是什么误会，他去派出所搞清一下就可以走了，他是一个律师会懂得如何与警察打交道。<br />
<br />
&nbsp; &nbsp;他们走后一会，大约14：00左右，我与沈纯理先生还站在小区的大门边，准备去吃饭。突然一辆深色的小桥车疯狂地朝我冲来，撞到我的左腿，紧急煞住了。周围的许多人都惊呆了，这个司机疯了。门卫奔出来要追究这个司机，一看是国保警察沈国良。他开进小区，又转过头，我走过去站在他的车前，大声谴责他：你想压人，我让你压。他又开过来撞我，我被撞出一步。这时民警小叶拉住我。杨浦区国保警察叶处长也在现场目睹此景。小区大门上空安装的摄像头记录了这个事件的全过程。<br />
<br />
&nbsp; &nbsp;国保警察沈国良离开时，又将我的朋友沈纯理先生带走了。在场的所有人都觉得沈国良的行为实在过分，今天没有人得罪他，我也没有妨碍他什么，他要找的郭律师也已跟便衣警察走了。他今天的行为已失去理智，公然开车撞人是一个警察可以做的行为吗？这表明他心里压力太大，已无法控制自己的情绪，工作的失败变成个人的泄愤，所做的行为与他的年龄与职业不相符，不适合做国保警察工作。<br />
<br />
&nbsp; &nbsp;沈国良先生是包管我的国保警察，对我的一连串打压都与他有关，还指使社区保安人员对我干一些坏事。我很清楚，但我总是谅解他，这是他养家糊口的工作。当然管住我，他有成绩，可以有奖励。但是，我坚守自己做人尊严、维护自己的公民权利，我不得不让他失败。过去他企图无法无理地软禁我在家，但被我冲破了。我回国的第一天他在接我的车上告知我不准接受记者采访，但我一下车就被记者包围，又接受了记者的采访。<br />
<br />
&nbsp; &nbsp;中国政府依法让我回国，实际上纠正了上海违法官员的错误。中国驻日大使馆代表中国政府一周内三次来远离东京的成田机场看望我，他们的诚意确实令我感动，我没有任何条件就主动离开机场，化解了一个难题。中国驻日大使馆也帮助我顺利回国。我们之间没有任何交易与承诺，是靠双方的诚信去解决问题。我的回国却让上海某些国保警察觉得很失面子，沈国良先生在我背后散布我与中国驻日大使馆有交易、有承诺的流言蜚语，还在制造一些麻烦。的确，他们一下子无法接受我回国的事实，但是个人、地方必须服从国家利益的需要。<br />
<br />
&nbsp; &nbsp;其实，沈国良先生不必太生气，这是工作，我们俩没有个人之间的怨恨。这个工作本来就很难做，他做不成功，其他人也做不成功，做成功的是不正常的。在一个法律制度日趋完善的国家里，要做违法的事，也只能穿着便衣，偷偷摸摸地干，对方有恐惧的心理，被吓住了，不讲法的警察就能得逞一下，否则很难成功，没有人肯屈服。沈国良先生就是要撞死我，也无法改变这个社会的现实与趋势。而且，中国的检察部门也不容许警察可以公然开车撞人。 <br />
