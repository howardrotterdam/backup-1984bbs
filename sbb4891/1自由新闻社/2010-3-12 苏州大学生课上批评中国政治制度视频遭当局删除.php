													<h2>苏州大学生课上批评中国政治制度视频遭当局删除</h2>
						
						
																											<div id="postmessage_513452" class="t_msgfont">苏州大学生演讲风靡网络遭删除（图，视频）<br />
<br />
2010-03-11<br />
<br />
一个据称是苏州大学课堂演讲的视频作品在网络上被疯狂转载，引起轰动，演讲者痛批了中国的政治制度，并调侃宣传机器和所谓的和谐社会，但此段视频不久之后被中国各大网站删除。自由亚洲电台驻香港特约记者心语的采访报道<br />
<br />
图片：苏州大学学生演讲揭示了中国当今社会的所有弊病 （心语拼图制作）<br />
<br />
星期二，一段标题是“苏州大学课堂演讲”的视频在中国的各个视频分享网站，例如土豆、优酷、酷6等被疯狂观看和传播，人们也在微博空间转发。演讲中一位同学（一说是教师）用大约25分钟时间来解释自己所设计的“人民日报大厦”，讽刺央视大楼，之后陈述了中国制度的弊病和根源，痛批了现行的政治体系，并分析了各种民主制度的价值。<br />
<br />
演讲的同学说，“中国到了如今这田地，官僚统治阶级贪赃枉法、腐败透顶，金融业黑洞烂账，资源破坏、环境污染，警匪一家，整个官场就是一个黑社会，房产改革、教育改革、医疗改革全部失败，成了压在十亿人民头上的新三座大山，两极分化成世界之最，信仰缺失……总之一起都在摸着石头过河。房地产‘摸着石头过河‘造就了几百个亿万富翁，花天酒地，醉生梦死，造成了上百万的拆迁户，黑白凸现的是强势既得利益集团与弱势民众的对立。”<br />
<br />
整个演讲轻松幽默，条理清楚，深入浅出，让很多观看者大为赞叹，他们表示“一贯被视为脑残大学生终于发声了，说出了真实的想法。”<br />
<br />
曾经在北京某大学担任客座教授的学者凌沧洲星期四就此向本台表示，他十分佩服这位学生的勇气。“我觉得这个学生是一个勇士，我希望媒体或推特能够把他搜索出来，这个孩子继承了邹容，就是革命军中马前卒那位勇士的风范，在万马齐喑中能够勇敢地呐喊，我很佩服他，作为在大学当作客座教授执教的人来讲，我都很佩服青年人的勇气，我也不能像他讲的这样生动，这么直白。”<br />
<br />
这名演讲的学生还陈述了中国腐败情况，他说：各地各级政府官员根本无需自己买车，他们可以用纳税人的钱去买豪华车---兰德酷路泽，烧免费的汽油，干所有的私事，还不用考虑吃交通罚单。在中国，有哪一天的新闻里没有贪官被双规、司法腐败、环境污染、矿难频发、民怨沸腾 ？ 而这些还是经过中宣部“过滤”、“漂白”之后的新闻呢。<br />
<br />
许多网友对该学生处境表示担心，有网友表示，“该学生想必已被喝茶，估计至少‘隔离’一个星期左右，写思想检讨书N份。”<br />
<br />
一位在厦门大学就读的陈先生告诉本台记者，“我现在很关心一个问题，他现在有没有受到什么威胁、处理，我知道厦大有一些老师，如果在课堂上讲这些的话，可能就会被学生举报，学生会认为是一种所谓反党反政府的言论，他们可能只是出于自己的价值观判断，会去举报你，所以我现在很担心这个学生。”<br />
<br />
有网友说，如果有这名学生的相关消息，希望大家及时发布，担心他从此被迫消失。<br />
<br />
这段演讲被放到各个网站仅仅一天，星期三，就几乎同时被删除了，在酷6网站上显示，“对不起，您访问的视频暂时无法访问”。<br />
<br />
曾在北京奥运火炬海外传递时，发表独立意见的留美中国大学生王千源对此现象表示：“现在在中共的很多理念里面，也经常用民主这个词汇，但只是代表表面的和谐意义，在美国的课堂上最主要的是能够发挥想象力，并能够有表达自己想法的权利，在课堂是受到鼓励的，大家确实可以畅所欲言，哪怕你的想法和主流想法不一致，反而可能更好，教授会觉得你比较有创意和胆量，你可以根据自己对一件事情的理解进行辨析。”<br />
<br />
以上是自由亚洲电台驻香港特约记者心语的采访报道。<br />
