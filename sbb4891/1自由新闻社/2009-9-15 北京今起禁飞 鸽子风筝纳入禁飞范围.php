																											<div id="postmessage_242085" class="t_msgfont">2009-09-15　来源: 北京晚报<br />
<br />
北京晚报9月15日报道 今天上午市公安局治安管理总队透露，自9月15日零时起至10月8日24时止，本市行政区域内实施禁飞，鸽子和风筝也纳入禁飞范围。即日起，(北京)本市的公安、民航、体育和空军等部门将对本市的信鸽公棚、飞行起降点、航模销售商店开展联合检查，违反规定的将依法予以处理。<br />
<br />
近日，北京市政府下发《通告》，自9月15日零时起至10月8日24时止，本市行政区域内实施禁飞，严禁各类体育、娱乐、广告性等飞行活动，并将鸽子和风筝纳入了禁飞范围。自9月15日零时开始，市公安、民航、体育和空军等部门对本市信鸽公棚、飞行起降点、航模销售商店开展联合检查。<br />
<br />
市公安局治安管理总队相关负责人介绍，今年禁飞除了往年规定的小型航空器以外，为确保国庆阅兵受阅空中梯队不受影响，将鸽子和风筝也纳入了禁飞范围：从9月15日零时至10月2日24时止，东起东六环，西至西四环，南起南三环，北至北三环之间的区域内禁止放风筝;本市行政区域内禁止放飞鸽子。记者了解到，除了国庆庆典彩鸽放飞工作照常进行外，其他信鸽比赛活动均已暂停。<br />
<br />
目前，涉及企业和单位均张贴了市政府禁飞《通告》，并与政府主管部门签订了安全责任书;飞行起降点已将飞行跑道封闭并设置了障碍物，航空器燃料已放空，飞行器具已被拆卸，伞具和动力装置已分别存放在有专人职守的仓库进行保管;信鸽公棚已停止了信鸽比赛和放飞活动。<br />
<br />
航模销售商店经营者表示，将全力配合做好禁飞工作，对前来购买的爱好者做好禁飞宣传工作，在禁飞期间对购买者进行登记，并及时将登记情况通报公安机关。<br />
<br />
据了解，公安部、国家体育总局等部门已联合发文，要求以天安门广场为中心的200公里半径范围内严禁各类体育、娱乐、广告性等飞行活动。<br />
<br />
在此，北京警方提醒广大市民，国庆期间请自觉遵守禁飞规定，停止一切飞行活动，规定区域内不放风筝，不放飞鸽子;举办参加庆典、促销、婚庆等活动时，禁止放飞各类充气气球;一旦发现空中有可疑飞行物或有人操纵小型航空器准备起飞的情况，请及时拨打110报警。<br />
