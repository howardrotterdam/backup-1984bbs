													<h2>方舟子：不要用“防甲流”中药集体毒害北京中小学生</h2>
						
						
																											<div id="postmessage_240147" class="t_msgfont">近日北京市教委在组织全市中小学生服用号称能预防新型甲型H1N1流感的漱饮中药，要求连服7天。今天我从报上看了其药方，吓了一跳：“每人份药量为：金银花2克、大青叶2克、薄荷2克、生甘草2克。”<br />
<br />
&nbsp; &nbsp; 这里最成问题的是“生甘草2克”。大剂量服用甘草的危害，国内外医学界早已知道，中外文献上有大量的报道，对此没有什么异议。甘草的主要成分是甘草酸，它具有和人体肾上腺皮质产生的激素醛固酮相似的作用，因此长期或大剂量服用甘草可引起“假性醛固酮增多症”：由于尿量减少、体内水分储存量增加，导致水肿；身体积存过量的钠引起高血压；血钾流失过多引起低血钾症，导致心律失常，肌肉无力。电子显微镜观察表明甘草能致心肌损伤。<br />
<br />
&nbsp; &nbsp; 甘草有类似雌激素的作用。孕妇服用甘草能导致早产。儿童服用甘草甜素片能导致乳腺发育。甘草能降低男子血液中睾酮的含量，导致阳痿、睾丸和阴茎萎缩。（具体文献见方舟子《常用中成药的真相――复方甘草片》）<br />
<br />
&nbsp; &nbsp; 根据实验和临床的证据，为避免出现不良反应，甘草甜素（甘草酸的钾盐和钙盐）的可接受量为每日每公斤体重0.015～0.229毫克。另一项研究认为甘草酸的可接受量为每日每公斤体重0.2毫克。根据后一项研究，一个体重60公斤的人，每天服用的甘草酸不应超过12毫克，甘草酸占甘草的含量大约是5%，即每天服用的甘草不应超过0.24克。按照北京教委提供的药方，其甘草用量高达成年人可接受用量的大约9倍，更何况是给未成年人喝的。<br />
<br />
&nbsp; &nbsp; 通俗地说，甘草是一种类似激素的药物，大剂量服用对少年儿童的危害更大。没有任何证据表明这个“漱饮中药”能对任何流感起到任何预防作用，最多只是起到安慰剂作用。但是做为安慰剂，首先必须没有任何毒副作用。北京教委组织中小学生服用已知有严重毒副作用的激素类药物，实际上是在毒害少年儿童。学生家长们为了自己的孩子的身体健康，应该加以抵制。<br />
