台湾立法院再次上演蓝绿大战！立法院昨召开临时会，审议两岸经济合作架构协议（ECFA）并且将草案交付二读期间，双方爆发严重冲突，拳打脚踢、水杯、垃圾桶满天飞。混乱期间，有立委被定时器击中脸部，血流披脸，也有人被推下台受伤。事后，蓝绿两大阵营均批评对方不是。<br />
<br />
立法院昨日上午就ECFA进行最后协商。国民党希望速战速决，打包审议，不要让ECFA横生枝节，但绿营则希望逐条审议，双方最终无法达成共识，立法院长王金平决定下午召开临时会商讨。会议开始，蓝营立委便一直占据主席台，让绿营相当不满，于是开始出手反抗。绿营立委试图爬上主席台，但被围在主席台的蓝营主委「击退」，于是便将议场内的杂物扔到主席台上，一时间议场内书本、水杯、垃圾桶满天飞。<br />
