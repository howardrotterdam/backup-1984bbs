<br />
最会议<br />
2009-09-19 12:44:03 　　来自: 叶子风 (越低俗越舒服) <br />
建国大业的评论&nbsp; &nbsp; <br />
<br />
　　 <br />
　　 这是一部非常精彩的电影，主题深刻，寓意丰富，至少有以下几个：“得会议者得天下”、“开大会者事竟成”、“只要你会开的比我好、什么都难不倒”、“不要迷恋哥，哥只是爱开会”，以及“最恨你那么久都不来叫我开一次会，最爱你当台上传来你的发言。最容易想起，最难忘记，最想要得到，最害怕失去，最初的陪伴，最后的需要，最远的距离，最近的心跳”。 <br />
　　 故事是这样的。正如西哲萨哈夫所言，每一次战争都是为了开好一次会，每一次会开不好都将引发战争。救亡战争胜利后，蓝营的张国立就邀请红营的唐国强前来，商量以后怎么开会的事情。本来，大家说好了，开大会，人人都能上台发言。但是蓝营内部有反对意见，认为这样人多嘴杂的，椅子也不够，开会不尽兴。张国立听从了这一派的意见，自己开会，不让红营参加。这还得了，不开大会，只开小会，唐国强答应，人民都不答应。那些粉红系的小营就更愤怒了，他们和蓝营红营不一样，手里没有枪，唯一的人生希望就是开大会，见者有份，他们才不会被落下。唐国强太了解他们的心思了，就向他们承诺：只要你们支持我把蓝营灭了，我主持会议，一定是人人有份，谁都能发言。这样一来，粉红系纷纷倒向红营，建立了“开大会”统一战线。很多学生上街，很多知识分子写文章，谴责蓝营“开小会”路线。蓝营内部也有分裂，对于张国立的小会作风多有不满，陈凯歌反在明处，王学圻和尤勇反在暗处。后来，外国两大营的态度都变了。美帝疏远蓝营，苏联力挺红营。此消彼长，红营掌握了局势，战场上节节胜利，会议也越来越多。从探讨辣椒和红烧肉，到争论队旗和校歌，话题广泛，气氛热烈。而在这一过程中，唐国强完全展示了他的天才，确切地说是开大会的天才，本来有不好意思参加的，都被他的魅力征服，统统前来鼓掌献花。最后是经典的大团圆结尾，唐国强主持了一次前所未有的盛大会议，从室内搬到了广场。大会胜利，小会灭亡，人潮澎湃，举国若狂。 <br />
　　 会议开不好，后果很严重。一部电影说出这样的道理不难，难就难在要说得有道理。韩三平做到了，他说的甚至不是道理，而是哲理。为什么蓝营实力强大，最后却惨败在红营手上？影片通过一系列细节对比，非常雄辩地告诉我们，这是历史的必然。对会议的态度不同，决定了成败。蓝营不是不开会，他们也开会，但都是坏会议，采用投票制，大家发言不踊跃，场面冷冷清清。红营就不同了，他们采取举手制，热热闹闹，争先发言，这是好会议。张国立性格不好，整天沉默寡言、愁眉苦脸，喜欢谈心散步，而不是开会。儿子陈坤也是这样，爱搞私访，开家庭小会。当然蓝营不爱开会，和他们的老婆比较辣有关系，下班必须回家慰妻。电影中有一幕，张国立的老婆去美使馆，门口的黑人士兵惊叹，她真辣！红营就大不相同了，绝大多数是单身汉，唐国强也是光棍，反正没见他爱人出镜。没有辣老婆，自然毫无牵绊，可以通宵达旦地开会。他们形式也搞得非常活泼，会前有人专门抓拍，会后一定合影留念。有时候开完会还会唱K，几个大男人喝完酒，抱在一起大唱《国际歌》，场面十分感人。和张国立相比，唐国强的性格真是太开朗了，完全是开会型的，谈笑风声，嘻嘻哈哈。他真是开会迷啊。有一次开会，蜡烛不够，他就吹灭了，说开黑会也要开。真的就像歌里所唱的“我最爱的就是那个会议，爱到可以去死，爱到整个世界灯全熄灭，最后还要给你体贴。” 还有一次，他开会太晚，第二天醒不来，敌人的战机来轰炸，他穿着睡衣就被担架抬走了，可是自己的炊事员，因为他没吃早餐，就留在厨房不肯走，被敌军炸死了。唐国强非常悲伤，想到那次会议被敌人利用，害死了厨子，就更加坚定了“枪杆子里出会议”的信念。这也像那首歌里唱的：“我最恨的就是那个会议，恨到可以去死，恨到快把自己的全部忘记，最后还要刺青铭记。” <br />
　　 毫不夸张地说，从来没有一部电影能把“会议”这个主题拍得如此深入。如果不是由一个擅长开会的人掌镜，恐怕根本就拍不出这么多神髓。完全可以肯定，没有人比韩三平更胜任此片导演――在官场里，他最艺术；在艺术里，他最会议。片中的演员表演也非常精彩，相对而言，港台明星逊色一些，演技有待提高，也许与他们平时缺少会议训练有关。但我们内地演员非常出色，每一次大会小会上需要的表情、动作和语气，他们全都演出来了。这里就不一一表扬了。但要特别指出的是，这些最精华的表演都是在不收片酬的情况下做出的，可见金钱并非动力，而是对艺术的腐蚀。所以，我建议以后拍戏最好不要给他们发片酬，如果有意见，就让他们开会。中国的事情，只要开了会，就没有什么解决不了的。</div>

							
							
							
							
