													<h2>刘沙沙：期待农民矿工发出声音的那一天</h2>
						
						
																											<div id="postmessage_68814" class="t_msgfont">――矿难、李刚、谈判中前进的中国。<br />
<a href="http://blog.tianya.cn/blogger/view_blog.asp?BlogName=liushasha_007&amp;ownerId=2016491" target="_blank">http://blog.tianya.cn/blogger/vi ... amp;ownerId=2016491</a><br />
刘沙沙博客<br />
一）不同层次的反抗<br />
　　<br />
　　李刚事件经过反复的冲突、镇压、封锁，在全国网民业主的汹涌抗议和奔走运作之下，终于在媒体和网络上解封。事情有了解决的曙光。在这个过程中，也有网友回贴：“去年煤矿死那么多人，也不见你们这些业主出来搞个帖子顶来顶去的，现在不就是伤了一个人吗？他的命比几百个矿工的命值钱？”对此，刘沙回复道：<br />
　　“你怎么知道,煤矿的事情中,发贴的,顶贴的,就没有这些业主呢?<br />
　　社会黑暗面,打击一处是一处,网络民主,争取一点是一点.每一个地方,每一处进步,都是社会的进步,我们还是一起努力好了！”<br />
　　<br />
　　说实话，对于李刚事件的某些报道和某些笔法，我还是小小不然的：“惊天血案、一个血淋淋的脾脏”这样的记述，也未免夸张。一个脾脏都是惊天血案，那么煤矿之恶劣状况，一砸就是几十人，上百人的血肉模糊，这又是怎样的惨境？而网上对煤矿的同情、无奈和叹息，却没有这次这么强烈，这么汹涌澎湃、前赴后继！原因很简单：矿工有几个有条件上网？一收买一封锁，他们的声音就被隔断了。而这次ＨＳ以及地方当局的做法，也使出了以前矿难操作的种种隔断措施：媒体禁言、分化收买、冲击捐款现场、掐断网络电视、开天窗掉线敏感字、广告粉饰、报纸抽页、种种丑态，样样来过！可他们却忘了，这次的地点，不是信息稀薄的穷乡僻壤，而是广州这样的通都大邑！以广州的信息流通之密集发达，搞这种封锁，要怎样的难度？何况业主这个中产阶层的交流和抗争能力，远非矿工村民可比！<br />
　　<br />
　　让我们设想一下，李刚事件若是发生在某某村又会怎样？某村村民维权被打了，致残了，村民的抗争受到开发商和地方政府的联合镇压，请问我们的村民有何手段？小小一个县通信公司，把网络一掐，方圆上百里立刻发不出网络消息。村民们现在也有了手机，可他们发出的消息能到达几个亲朋好友？能到达什么样的亲朋好友？对上层能有多大的影响力？武警把几条道路一封，立刻记者进不来，村民出不去。偶而有几个漏网之记者混进来了，啊啊地方政府还可以宣布，谁举报一个记者，奖励多少钱！立刻会有人把“多管闲事”的记者给举报了！大家别笑，前几年河南艾滋村的消息，就是这么封锁的！<br />
　　<br />
　　而这次合生和当地政府的一些人，未尝不想这么搞！其实已经在这么搞了！只是全国的业主网友，个个都感觉到自己受了威胁，死命地抗争！就这，前几天广州的封锁，各大网站的步步沦陷，还是让大家捏了一把冷汗！就如刘沙本人，在关注的同时，也在不停地犯嘀咕：网易完了，天涯封了，凯迪干脆连一点消息都不见！可别这次为了个两会，真的把这事给捂灭了吧？还好广州城市的规模使得ＨＳ以及当地政府黔驴技穷：封得了华南新城的网线，封不了整个广州的网络！就算是出动武警封锁华南新城，那么合生其它楼盘呢？广州其它楼盘呢？难道全面封锁？笑，那可就是广州戒严，到了这一步，合生是再也拱不动了！而另一方面，全国的网友业主在极力抗争，网络、电话、种种浩大声势，终于造成了压力。这还是公开的抗争，就私交来讲，业主阶层的私交圈子也比矿工村民更接近上层，更能通过私交的渠道联络上层：“这件事情错了！到了非管不可的地步了！”这才有了中央台的报道，各网站的解禁！<br />
　　<br />
　　二）“业主立宪”以及未来中国的谈判<br />
　　<br />
　　就刘沙的态度：李刚事件，远没有煤矿的状况那么恶劣严重。然而身为民主主义者，对李刚事件，始终应该热情支持，原因是：在中国目前的条件下，业主维权，在中国民主建设的过程中，是一条法理上有依据，操作上较成熟，比较现实，比较容易成功的操作方式，并且，业主维权的种种实践，形成的种种经验和制度，对中国以后的宪政改革、谈判方式等等，能提供一个蓝本：这个蓝本，也许幼稚，也许温和，但却是“中国的、自己的”民主实践成果。这就是“业主立宪”。大家不用笑：从法理上来讲，宪法就该是这样制定的！――住在一起的一群人，合起来一起讨论关乎大家利益的公共事务，形成规章，表决通过，委托给专人管理――宪法就该是这样产生的！国家就该是这样产生的！<br />
　　<br />
　　另一方面，随着社会的进步，权势利益阶层不得不向底层民众分肥：提高薪金，农业补贴，等等。等到将来，工人农民手中的财富，上了一个特定的台阶，有了强劲的通信交流能力――如这次网友业主这样，有了锁不住的通信交流能力，那时他们有条件发出自己的声音，则现在“业主制宪”的谈判博弈机制，工人农民自可借鉴之，实行之。那时，就是中国宪政改革的时机到来。<br />
　　<br />
　　只是在此之前，中国的民主力量，一方面要防范权势阶层的反扑，一方面也要尽量防止暴力革命：倒不是暴力革命缺乏正义和正当，也不光是因为人权诸权中生命权最高，也不仅是因为中国经不起动荡，更重要的是：暴力革命的过程中，势必要使用残酷手段，要建立战斗队伍的组织性、纪律性――服从性，而所有这些，在革命成功之后，很可能又会变成限制民权的桎梏，暴力手段很快又会落回民众的头上，这种悲剧，法国革命、俄国革命、中国革命，苦笑，我们已见识了好几遍了！看看我们的几千年历史就知道了，对于中国，陈胜吴广不是真正的革命，辛亥、四九也尚未成功，对于中国，真正的革命，始于谈判，始于契约。<br />
　　<br />
　　三）风险与激荡<br />
　　<br />
　　当然，“富于革命传统”的中国人民，面对特权阶层，反抗的威胁是要给的，革命的旗帜是要打的，总之，“不承诺放弃武力”！坏笑，所谓取法乎上，仅得其中，所谓漫天要价，就地还钱！中国的形势就这样，你说这屋子太闷了要开个窗户，他们就有种种理由给你拖，可你说他妈的老子要掀房顶了！他就会同意开窗户～～<br />
　　<br />
　　我是那个要掀房顶的人。<br />
　　<br />
　　嘴里这么嚷嚷，心里想的还是开窗户，而不希望中国走到掀房顶的地步。<br />
　　<br />
　　原因么，上文已有涉及。托尔斯泰反对革命，曾和年轻的革命者说过他的忧虑，真不幸，他的忧虑条条言中！――“年轻人，通过暴力，不可能建立一个符合道德的制度，一旦你们掌握了武器，你们也会很快建新的专制主义。你们不是破坏专制，而是使它永存下去。”<br />
　　<br />
　　而年轻的革命者回答说：“俄罗斯的大地上，罪恶和黑暗已经太久了！贵族们的罪恶，已经剥夺了他们自己和他们后代的生存权利！贵族阶级，以及他们的子子孙孙，都要在俄罗斯的大地上被消灭！”天涯的激进民主主义者，也曾有言“让民主主义者的后代永远生活在民主中，让专制主义者的后代永远生活在专制中”，这种令人毛骨悚然的偏激，就是我下文要说的，“年轻人绝望的愤怒”。一旦中国的专制形势恶化到了暴力革命的地步，一旦这种报复心理成了民众普遍的怨愤，那才是中华民族莫大的不幸。<br />
　　<br />
　　然而，如果特权阶层的反扑，到了压抑窒息的地步，则“一个年轻人，如果意识到直到自己老了死了，这个社会的某些不公都不会改变，那么他有理由，对这些不公，产生绝望的愤怒！”前几天看到广州媒体的被禁口，我还曾寄希望于网络，毕竟还有网易、还有天涯。等到网易沦陷，天涯封口的时候！我的第一个反应就是：已到了最后关头了！若是连网络都全面封锁的时候，就得联络网友抗争了！幸亏天涯的华南新城专版，一缕袅袅微弱的火苗，半死不活地袅袅了几天，又烧大了，电视台也解禁了！这样，让我对中国的民主进程，又有了信心――<br />
　　<br />
　　有人会因此说刘沙是机会主义者，一点小事就会改变主意。我觉得这很正常：从微积分的观点来看，任何宏观叙事都是微观叙事，任何历史都是瞬间，任何革命，落实到每一个参与者身上，都是一系列体验、判断、愤激、抗争的总和。千千万万“个体”的选择，构成社会的进步――我的态度：政府对言论放松一分，我对它就承认和尊重一分，它一封锁，我对它的批判就更严厉几分！我有这样的权利，每一个公民都有这样的权利！<br />
<br />
ＰＳ之一：有些网友的“跪求公道”，遭到平等主义者怒批。但是，细想想，所谓天理国法，无非人情之常――跪求云云，作为煸情，又何尝不是一种交流方式？<br />
　　<br />
　　ＰＳ之二：我的政论文中处处都是“刘沙以为”，以前有网友指责我自恋。而我的观点是：政论文中动辄使用群体名词，实属越权！一张口就是“ＸＸＸＸ人民如何，广大业主如何”，谁给你授权了？你通过何种程序得到授权了？所以，写政论时注明“本人认为”，这不是自恋，恰恰是一种谨慎。<br />
　　<br />
　　ＰＳ之三：本贴对两个人砸来的砖，不做答复：一个南宫得一，一个是吴秀夫。不答复南宫得一，是因为尊重：我很少看到这样态度认真而口角干净的ＦＱ，因此他砸的砖，我都要深思熟虑后，另起一贴来答复。不答复吴秀夫，则纯粹是鄙视：吴秀夫其人和我并无交锋，他并不曾得罪刘沙，鄙视他是因为他在其它地方的一些发言，实在太鄙陋了！<br />
　　<br />
　　ＰＳ之四：托尔斯泰《逃向苍天》的那段台词，我记得不太准确了。在网上遍觅而不得。谁有原文的话，发给我，不胜感谢！<br />
　　<br />
　　ＰＳ之五：本人准备向李刚捐款五十，以示对民权运动之支持――尽管我的经济条件，远没有李刚好，尽管，看了下面这张贴大家就知道，刘沙确实是没钱了！苦笑。<br />
