													<h2>南阳刘沙沙上街散发《宪章》，失踪ING</h2>
						
						
																											<div id="postmessage_68712" class="t_msgfont"><a href="https://www.cmule.com/viewthread.php?tid=212732&amp;extra=page%3D1" target="_blank">https://www.cmule.com/viewthread ... &amp;extra=page%3D1</a><br />
<br />
刘沙沙的短信息：<br />
<br />
我刘沙沙（刘琳娜），我在南阳，梅溪宾馆，我已做好了一切准备，烧毁了文稿，砸碎硬盘并扔进了长江......破釜沉舟，一往无前，走上街头，散发宪章，我在几个群的共享里已经上传了这次行动的宣言：“让大家慢慢习惯。”以及宪章的两个传单版本，以及我的照片，请大家广为转发。<br />
<br />
刘沙已准备好迎接这次行动的一切后果：被传讯，被停职，背井离乡，流落天涯，被捕入狱，等等。如果我被捕，我会从第一天开始绝食抗议。敬请关注！我另一个电话是15838757372我单位电话是 0377－63849239，63849248。我父母家电话是0377－68227575。<br />
<br />
如果我没事，请不要惊扰我父母。如果我失去联系，超过三天，请和我父母联系。不过，我妈妈是个普通市民妇女，我爸爸是个毛左共产党员，在当局的压力下，他们很可能倒向政府一边，而不会向大家透露我的狱中抗争。总之，我的政治态度，以我本人的网上宣言为准，我不授权我的任何亲人向外发布我的政治态度。<br />
<br />
我的任何亲人发布的“有利中.共之说法”，均不代表我本人意见。<br />
<br />
---------------------------------------------------------------<br />
<br />
<br />
河南刘沙沙上街散发《零八宪章》传单疑被控制<br />
<br />
<br />
她的计划是今天上午走上街头，现在（4月4日14：00）短信电话均联系不上她，相信是已经实施了计划并已经被控制。<br />
<br />
附：刘沙沙：让大家慢慢习惯<br />
<br />
<br />
《零.八宪章》传单，四百份。洁白坚硬的纸页，放在面前。<br />
<br />
决心走上街头之前，我烧毁了手稿，砸毁了硬盘，我这样做的时候，一次次心如刀绞。常常剧痛得做不下去。<br />
<br />
倒不是要毁灭什么犯罪证据，我的政治观点并不激进，硬盘里没有任何犯罪痕迹。可是--<br />
<br />
我记录的“网友谈话”，某些网友，某时某地的愤激之语，说过就忘，风中消逝的愤激之语，如果将来在“政治犯刘沙”的记录中被查出，引起重视，将来，会给他们带来麻烦。<br />
<br />
这些妙语如珠的交锋，时代和思想变化的见证，如果毁掉，我心疼。硬盘里存的情感日记，那些美丽柔软的心事，如果毁掉，我更心疼！可如果不毁掉，天哪我不敢想象一群五大三粗的警察，横眉瞪眼来我家抄我硬盘，翻看我文件的场面！那份耻辱和痛苦，想一想就无法忍受！<br />
<br />
可，这耻辱可怕的前景，让我痛苦，却不能让我退缩，而是让我激怒！我不要过这样的生活，我不要我的子孙后代过这样的生活，仅仅是发表意见，仅仅是“表达”，就要遭受公权力如此的凌辱！<br />
<br />
下决心走出这一步，还因为，被当局的重重封锁、种种猥琐而激怒。<br />
<br />
“ 言论自由”已是当今世界的政治常识。而中国的当局，却通过重重言禁，一再地侮辱常识、激怒常识。自由竞选、多党政治、让老百姓“货比三家”--这已是当今世界的政治常识，而我们的当局，从“新青年学会”到“中国民主党”到“中国新民党”一而再、再而三地抓人，诬以重罪、施以重刑，一而再、再而三地侮辱常识、刺痛常识！<br />
<br />
当新民党主席郭泉被诬以“泄露国家机密”之罪的消息传来时，我始而恐惧、继而激怒：一个文学院教授，文学啊，教授啊！无关理工，无关科技，你们竟然栽他个“泄露国家机密”刑拘至今，亲人也不让见、律师也不让见、连公开审判都得不到！泄露国家机密，这么宝贵的罪名，这宝贵武器，你们能不能留给“真正需要的人”？能不能留给靠点谱的人？弱智如此！无耻如斯！<br />
<br />
我们公民，在中国的政治高压和惨痛往事的阴影下，刺痛、担忧、惊恐、激怒。可，换个角度想想，我们的当局，难道不也是在全世界的民主包围和惨痛往事的阴影下手足无措、一夕数惊？这互相惊吓、互相激怒的死扣，还能不能解开？<br />
<br />
还好，毕竟时代在前进，政府和民众都在学习，都在成熟--０７年的厦门６.２游行，郑州６.６事件，都奇迹般地以政府的让步而和平收场。贵州瓮安事件，重庆的士罢运，官方公开新闻同时，都有检讨让步。《０.８宪章》传播至今，政府也只是扣了晓波一人，约谈各地“签友”，也尽量保持着礼貌，保持着交流、宽松。而贵州德江“龙灯冲突”之解决，则让人看得不禁莞尔--我们的政府和民众，都在慢慢意识到，没有什么你死我活，没有什么不共戴天，新闻公开，天塌不下来！出来见群众，也不是世界末日！　~_~<br />
<br />
我们能理解政府机关转变惯性的艰难，我们也能理解，如此庞大的机关在学习新事物时的要面子和手足无措。我们还能看出，政府是想“拖”，拖得大家对《宪章》都没了热情，是想用封锁和时间来考验《宪章》。如果“宪章派”们没能通过考验，被拖熄了热情，政府会在松一口气的同时瞧不起我们！而，这样一拖就烂的运动，也难怪人家瞧不起！活该国内外瞧我们不起！<br />
<br />
想要人尊重你的理想，你必须为理想而战斗。“如果《宪章》对你们这么宝贵，你们为什么不起来为宪章而奋斗呢？如果言论自由、多党政治对你们这么宝贵、这么常识，得不到就痛，那你为什么不行动不战斗呢？”<br />
<br />
还好，这个世界还是看到了，当我们的常识被侵犯时，我们的疼痛和反抗：宪章一直在各种渠道里扩散流传，各地的建党运动连绵不断，笔者曾一天之内见到了三个新组建的党！令人莞尔--这种几个人、几个人的小党，目前对政府还没有什么威力，然而，不停地在建党，这就是一种姿态，这就是常识的、韧性的力量！低调的、汪洋大海的力量！--<br />
<br />
让我们血性而又理性，坚定而又温和，此起彼伏、前赴后继--野火春风、春风化雨、雨夜听潮：让人权的潮水慢慢地、慢慢地上涨，让“文明”的细雨慢慢地滋润万物，给各方时间去妥协、去学习，给政府时间让他习惯--习惯那点点野火一样处处冒出的党派，习惯那一次次游行、一次次请愿之后的谈判，习惯那罢工罢课之后对穷苦员工的妥协，习惯我这样，漫天里纷披而下的雪片传单。</div>

							
							
							
							
