													<h2>《应对警方，保卫宪章》 作者：刘沙沙</h2>
						
						
																											<div id="postmessage_68718" class="t_msgfont">(博讯 boxun.com)<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 签名之后，本人在等待喝茶，这是设想的“和警方对话”。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 进入警方办公室后，开场白：<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; “开始之前，我讲两件事，两个原则：<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 一，我还是把共产党当做可以交流、可以说服的对象的，并且民主就要讲诚信和公开化，所以今天你询问我本人的行动、观点、设想等，我答你的将全是实话，是真实意思。我在你们跟前怎么说，出去还是怎么说、怎么做，会一以贯之。而，另一方面，贵党和现政府还是有许多落后于时代的地方、不能很好保护人权的地方，并且存在着侵犯人权的可能性，所以――我的第二条：<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 二，事涉他人，一字不提！<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 我的朋友们说了什么，做了什么，都是属于人家的言论行为自由。公民不曾明确授权给政府的权利，推定由公民保留。所以，我的朋友们不曾明确授权向政府发布的言行，推定属于人家的个人自由，我无权向你们发布。“<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; …………<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 警：你签了零八宪章吗？<br />
&nbsp; &nbsp; 我：签了。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 警：你是在什么地方看到宪章的？<br />
&nbsp; &nbsp; 我：根据原则二，我不回答你。<br />
&nbsp; &nbsp; 警：“在什么地方看到的”都不能回答？<br />
&nbsp; &nbsp; 我：对，我不回答你。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 警：零八宪章是一个反党反社会主义的反动纲领。<br />
&nbsp; &nbsp; 我：什么是反党？我本人是唯物主义者，百分之八十的马克思主义者，在世界观上和共产党并无不同，不同的是方法论。我反对的是那个一党专政之专政，是那个通过宪法规定的一党专政之地位。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 我不是逢共必反，我心中的民主框架里，宪章的民主框架里，共产党仍然要作为一个合法政党而存在。如果共产党在民主竞选中能说服民众、能拿到足够选票，仍然有资格上岗执政。这样的民主设想如果也被你们看成是反党，那么，反了就反了！一个拒绝民主改革的共产党，一部分拒绝民主改革的共产党人，我反了就反了，（调侃）打倒共产党反动派！　　~_~<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 其次，什么是反社会主义？又什么是社会主义？如果是马列毛泽东所定义的公有制社会主义，那么１９９９年全国人大九届二次会议通过的宪法修正案，声称“非公有制经济是社会主义经济的重要组成部分”，那才是反社会主义呢。或者２００２年中共十五届七中全会修改党章，允许私营企业主入党，那才是反社会主义呢，你去找他们算账去！（笑）<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 如果社会主义是邓小平定义的“社会主义就是发展生产力”，那么资本主义，自由主义“在它统治世界的一百年时间里呼唤出来的生产力，比过去一切世纪的总和还要多，还要大”。并且至今还在呼唤，源源不断地呼唤，呼唤出来的新生产力，眼花缭乱、层出不穷――资本主义才是邓小平所定义的社会主义，你们啊，先去给“邓走资”把这账掰清了再说。（笑）<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 如果社会主义指的是民主框架下适当的经济调控＋累进税＋福利制度这样的社会主义，就是恩格斯晚年所主张的社会主义，那么，这样的社会主义正是我所赞同的，也正是体现在了宪章中的，这时你说宪章“反社会主义”，又从何说起？<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 警：宪章的本身没有问题，重要的是，它是由谁提的！<br />
&nbsp; &nbsp; 我：太对了！宪章本身很普通，重要的是，它是谁提的！如果宪章的这些原则，“民主、公正、福利”这些话是共产党提的，那我会哈欠连天“哎呀老生常谈了又，应付民众了又！”，而，现在是由刘晓波王丹来提，由另外的一群人来提，有另外的一群人站出来和中共争夺这“民主公正”的话语权，在民众跟前，有另外一群人来竞标，太好了，太对了！就该这样！就不能只让中共一家竞标！老百姓有货比三家的权利！<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 伟大的共产主义战士切.格瓦拉，在玻利维亚山区打游击的时候，在牺牲的前几天，曾经跟当地农民讲过：“当我们走了，资产阶级政府会来到这里，会给你们带来医院、公路、小学。他们这样做，不是因为他们愿意这样做，而是因为，我们来过了。”<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 而现在，在中国风雨飘摇的经济形势下，官僚资产阶级的中共政府，能关注就业、维护劳工，能拿出九十亿来慰问贫困群众，很大程度上，不是因为他们愿意这么做，而是因为，我们斗争过了。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 警：搞宪章的人是一群里通外国别有用心的人！――<br />
&nbsp; &nbsp; 我（正色，热血上涌！）：对不起，说到这里，一步不退，誓死保卫！<br />
&nbsp; &nbsp; 一，所谓里通外国。一个新思潮、新运动，当它不是在本国先兴起，而是在外国先兴起的时候，后发国家的新运动，肯定要在思想、人员、组织甚至资金各方面接受先进国家同志的帮助。俄国十九到二十世纪的社会主义革命，一直在接受西欧社会主义者的帮助，接受第二国际的帮助。中国共产党在建国前的斗争中，一直在思想、人员、物资、党建各方面接受苏联的帮助。鲍罗廷到中国来干什么的？一九三六年红军西征，是为了接受苏联援助的武器，一九四六年东北，大批的苏联物资都给了林彪！（对警察）你别急！――这没错，这没什么不对，工人无祖国，革命无国界，共运史的跨国界行动，并不损害它的“历史的正义和正当”。那么民主运动的跨国界行动，也并不损害它的正义和正当。我承认马克思主义的普遍原理，也请你们承认民主主义的普遍原理，或者说，我承认马克思主义的普世价值，也请你们承认民主主义的普世价值。否则，凭什么马克思主义是普遍真理，而民主主义就不是？凭什么？你说了算？中共说了算？<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 二，所谓“别有用心的人”，你是指刘晓波王丹他们吧？那么――（热血沸腾热泪上涌！）――事涉刘晓波王丹，你不要和我讲任何道理！因为共产党在他们的身上，在陆四的事情上伤了天害了理！至今还在伤天害理！是共产党把一场运动变成了悲剧，把一群凡人逼成了英雄，把广场，用鲜血，变成了图腾！在陆四不能公开讨论，不能交由人民讨论的情况下，现在，在这个办公室里，你说他们任何坏话我都不信，你说下大天来，奈何我不信！刘晓波和王丹，他们事前理智，事后负责，是条汉子，是我心目中的英雄！是我愿意热血追随的领袖！提起陆四，我是摆明了要跟共产党打这个擂台！我知道这话的严重性，我害怕，我害怕得浑身直哆嗦，但是――我想起先烈，我就不怕了！――<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 警：陆四是反革命暴乱――<br />
&nbsp; &nbsp; 我：好啊，有理你拿出去讲啊？镇压反革命暴乱，这么理直气壮的事情，你们拿出去说啊？讲啊？宣传啊？抗日战争，解放战争，纪念日一个接着一个，电影拍了一部又一部，而陆四，好，平息暴乱，保卫共和国，这么光彩的事情，你们怎么不说啊？不提啊？如果说“平反陆四”这说法太难为你们，太先入为主，那么换个说法，公开，公开行不行？允许你们批判，允许你们表功，允许人民讨论，行不行？党报军报上讨论，你们不愿意，那么允许民营媒体讨论，允许网友讨论？行不行？新浪、搜狐、网易发个新闻大家讨论行不行？天涯凯迪、铁血强国、腾讯西祠六大论坛，允许关于陆四发出主贴，哪怕是你们发出镇压有理的主贴，下边不封不删，允许民众讨论，行不行？你们有没有这个胆量？咱们掐着表看看讨论陆四的主贴，哪怕是骂王丹的主贴，能生存几分钟？！我在各大网站上蹲了四年，四年的调查取样证明，你们没这个胆量！那么好，陆四一天不公开，我追随刘晓波王丹一天，陆四一天不公开，刘晓波走到哪一步，我跟到哪一步，王丹走到哪一步，我跟到哪一步！<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 警：他俩也不代表绝对真理啊？<br />
&nbsp; &nbsp; 我：我知道。陆四澄清之后，民主改革成功之后，我会反思的，我也会骂他们的。但，不是现在，不是共产党还伤着天、害着理的现在。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 警：你会骂他们？<br />
&nbsp; &nbsp; 我（疲倦地笑笑）：当然啊。民主改革成功之后，当他们能站在中国人民面前的时候，我会骂他们的。会比骂共产党更厉害，会比共产党骂得更厉害。所有你们现在骂刘晓波的话，刘黑手什么的（笑），我都会扣在他头上，就试试他的胸襟和成色！我现在拼命支持刘晓波，就为了一个将来能在论坛上骂刘晓波的权利。就为了一个将来能在论坛上骂刘晓波而不掉线不敏感字不封ＩＤ的权利！<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 警：不谈刘晓波了，谈宪章本身吧。宪章第十八条是分裂祖国，你这样走下去会很危险的！会犯“分裂祖国罪”的！<br />
&nbsp; &nbsp; 我：你是指联邦制？第一，联邦制不是分裂祖国。美国是联邦制，分裂了吗？没有。俄罗斯是联邦制，分裂了吗？没有！联邦制不是分裂祖国，相反地，在某种意义上说，联邦制是一个可行的统一祖国的方案！从政治学的角度来讲，中央给台湾的政策，保留军队什么的，那都甚至不是联邦制了，那比邦联还松散了。如果联邦制就是分裂祖国，那你去跟中央抗议去！（笑）<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 第二，关于联邦制，我签名的时候就声明保留了。前边我说了联邦制的利，但，另一方面，我也看到了联邦制的弊端：联邦制不是分裂祖国，但是，联邦制的效率较低。联邦制不是分裂祖国，但是，联邦制增加了分裂祖国的风险。这些意见，签名的时候我就声明了，并且在国内，在多个场合，在我的博克里，多次表示异议。可惜我的博克被你们删了又删，这就不能怪我了！我反对分裂祖国、维护祖国统一的文章都要被你们删除，这个账，得算到你们头上！　~_~<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 警：那你现在能不能写个声明，声明反对宪章，反对宪章第十八条？<br />
&nbsp; &nbsp; 我：不行。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 我要批评宪章，必须得在外边，在自由讨论的环境下。在外边我已经批评过了，我批评了又批评，可都被你们删了！而现在，在你们这个办公室里，不管你怎么说是交流啊讨论啊，警察办公室，无论如何都构成一种压力和胁迫。在这里，我不会写下任何批评宪章的文字，一个字都不会写。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 要我批评宪章，在网上批评宪章，可以――你得允许我张贴宪章全文，然后我发出对宪章的三条保留意见。你得允许宪章的写作者反驳讨论，然后我会发表批评意见。象现在这样，你们把刘晓波给扣了，不让宪章作者们发言，封了人家的口，捆了人家的手，然后组织我，刘沙，来搞批判，打瞎子骂哑巴，这种缺德事儿，我干不出来。你们忍心，你们去干！<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 警：你认为宪章会成功吗？<br />
&nbsp; &nbsp; 我：目前，不会。二十年之内都不会。也许尽我一生都不能看到宪章成功。但是，正因为尽我一生都看不到宪章的成功，它才值得我去奋斗。这才是站在黑夜里的火把，站在时代前沿那一份先驱的自豪感！――我不喜欢太容易得到的幸福。（笑）<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 如果宪章现在有了广泛的群众基础，已经成了群众运动，眼看就要成功了，那我只会乐呵呵地站在一边看着。甚至我要出来批评宪章，要往群众头上浇冷水了――为的是防止这群众运动走向另一个极端，简单民主化的极端。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 警：你也承认宪章没有群众基础了，那你为什么还要推行这个没有群众基础的宪章？你这岂不是不民主了？<br />
&nbsp; &nbsp; 我：大清国的子民还普遍认为“这大清国的天下不是我们大家的。”那怎么办？秋瑾孙中山他们就不革命了？<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 何况，中国老百姓目前普遍的民主素质不高，你觉得这是谁的错？建国至今六十年了，改革开放也三十年了，普遍义务教育也十几年了，中国老百姓的政治素养，如贵党所说，仍然低下得连个普选都搞不好，这是谁的错？从小学到大学的政治思品课程，社会主义，共产主义，马列毛邓三，拼命往学生脑子塞的同时，对学生有过起码的民主教育吗？多党竞选这类民主原则，在学校课本里，哪次不是被批判的对象？如果说，对于民主宣传，你们不愿意去做，那你别拦着别人做，好不好？放松媒体管制，让别人宣传好不好？至今中国老百姓听不到另一种声音，这是谁的错？对另一种声音，对零八宪章，封杀得如此严厉，这是谁的错？执政六十年到如今，中国百姓的民主素质、中国民主状况还是如此落后，难道不是党和政府的耻辱？　<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 警：你能退出宪章吗？<br />
&nbsp; &nbsp; 我：宪章是一份文件，一份倡议书。不存在所谓“退出”。你这是一个谓语和宾语不搭配的病句。<br />
&nbsp; &nbsp; 你是不是指围绕宪章形成的组织？那样的组织，据我所知，目前还没有，如果有――（认真地盯住警察的眼睛，郑重地，坚决地）――如果有，我会参加的。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 警：你能不能保证，以后不再宣传宪章？<br />
&nbsp; &nbsp; 我：（苦笑）：我多想欺骗你一下啊，欺骗你一下让你能完成手续，大家都好过得去。可是，对不起，正因为我还把你们当朋友，正因为我还把中共当做一个可以说服交流的对象，所以，我对你们坦诚，我不欺骗你们――（坚决地）我不放弃。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 我不放弃宪章，不放弃宣传宪章，不放弃宣传宪章的权利。对不起。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 警：你不怕因此连累父母？你父母都那么大年纪了让他们为你担惊受怕？<br />
&nbsp; &nbsp; 我：我当然害怕。但是，自古忠孝不能两全，“爱国者随时要准备，为了忠于国家而反对政府。”如果你们因此惊动我父母的话，那就是当局在人权上又后退了一步，这个账，仍然要算到你们头上。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 警：你态度这么差，你是要承担后果的！<br />
&nbsp; &nbsp; 我：我不会主动去和共产党寻衅，但，某些权利，某些底线，不放弃，不后退！我等着你们的“后果”，我本来是凡人，我等着你们制造英雄。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; …………<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 以上，是我设想的“与警方对话态度”。面对警方，刘沙将按本文去做，去坚持态度，坚持气节。但，本人不强求其它“签友”――战俘是没有所谓背叛的，战俘在胁迫之下发表的言论，不受追究。民主主义首先要关注人道，关注每一个人的人权。当“签友”们面对当局的人身财产威胁时，民主主义允许大家首先保护自己。“国旗保护她的每一个公民，包括那些蔑视她的人。”“宪章保护她的每一个公民，包括那些‘背叛’她的人！”</div>

							
							
							
							
