2009年4月4日上午十时，《零八宪章》签署人之一，河南省南阳市中石化职工刘沙沙（刘琳娜），给朋友们传出两封信后，烧毁个人书写的文稿，并将电脑硬盘砸碎，扔进江中，“破釜沉舟，一往无前，走上街头，散发《宪章》”。离开居住地前，她写了《让大家慢慢习惯》一文（附后），试图通过自己的行动，让人们习惯言论自由的政治常识，“给政府时间”，让它开始习惯――“习惯那点点野火一样处处冒出的党派，习惯那一次次游行、一次次请愿之后的谈判，习惯那罢工罢课之后对穷苦员工的妥协，习惯我这样，漫天里纷披而下的雪片传单。”<br />
