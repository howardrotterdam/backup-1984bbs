<br />
<br />
　　上世纪50年代，刘允斌回国，在内蒙古包头参加了我国第一颗原子弹的研制工作。1958年，玛拉带阿廖沙来中国居住了十余天，回苏联后与刘允斌离婚。当时，由于中苏关系紧张，玛拉母子与刘允斌失去了联系。<br />
<br />
