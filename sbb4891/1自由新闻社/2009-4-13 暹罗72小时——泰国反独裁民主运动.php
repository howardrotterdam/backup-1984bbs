																											<div id="postmessage_74173" class="t_msgfont">[个论]陈季冰专栏:稚嫩的民主和撕裂的社会<br />
 <br />
 <br />
日期：[2009年4月14日]&nbsp;&nbsp;版次：[AA30]&nbsp;&nbsp;版名：[个论]&nbsp;&nbsp;稿源：[南方都市报]&nbsp; &nbsp;网友评论： 9&nbsp;&nbsp;条 <br />
 <br />
&nbsp;&nbsp;<br />
 <br />
　　■媒体思想之陈季冰专栏<br />
<br />
<br />
　　如果说泰国今日的政治社会乱象对这片风光旖旎的旅游胜地造成了无法弥补的伤害,它也造就了唯一的“赢家”―――他信。自从2001年2月9日他信当选泰国总理以后,这个笃信佛教的和平国家似乎就无可挽回地走上了分裂的歧途,而2006年9月19日泰国军方趁他信出访而发动政变、解散政府并废除宪法,则给这个素来以温顺的笑脸呈现于世人的国家点燃了一把烈火。现代历史上泰国曾发生过难以计数的军事政变,但从来没有一次像2006年夏天推翻他信内阁的政变那样,全面而深刻地展示了一个稚嫩而脆弱的新兴民主社会的制度以及―――更为根本性的―――精神缺陷。<br />
<br />
<br />
　　我同意许多观察者的观点,泰国问题植根于严重的贫富分化,多年来的经济高速增长不仅没有提升下层民众的经济地位,反而使这种巨大的社会不平等日益触目惊心。这并非他信造成的,相反,身为泰国权贵阶层中金字塔顶端的一名巨富政治家,他信试图以其雷厉风行的个性风格和纯熟高超的政治技巧在比较短的时间内扭转这种状况。这使得腰缠万贯的他信具有讽刺意味地赢得了大多数泰国底层穷人的爱戴,同时也不可避免地得罪了曼谷各领域的精英、军队,甚至王室。为了保住自己的权势和顺利推行其政策,他信采取了带有明显民粹主义色彩的政治鼓动手腕。正是后者,在泰国社会和泰国人民心中撕开了一道伤痛的裂痕。<br />
<br />
<br />
　　政治、经济、宗教、种族和文化地位的不平等以及由此滋生的对现行体制和政策的不满在世界上任何一个国家和地区都广泛存在。与成熟的西方老牌民主国家非常不同的是,在新兴民主国家,政客们改变这种状况的努力往往是从“建构”社会对立开始的。一旦这个潘多拉盒子被打开,整个社会就会受害无穷,甚至连最初的民粹鼓动者自身也终将被其掀起的滔天巨浪所吞没。<br />
<br />
<br />
　　如果把发生在新兴民主社会和成熟民主国家的集会、罢工、示威等对抗性政治活动作个对比,我们会发现一个根本性的区别:在成熟民主社会,示威者反对的从来都是政府的某一项具体政策；而在新兴民主社会,示威者反对的永远都是现任政府本身。这实在是一种异常深刻的悖论。在成熟的民主社会,有头脑的公民普遍赞同一条原则:凡是能够不用投票箱解决的分歧最好不要投票。因为他们深知,投票必然意味着多数对少数的强迫,必定会造成被压制者的不满；而协商和妥协虽也不能达到皆大欢喜,但最后的结果至少是各方自愿接受的,不致留下很多后遗症。这条原则的反面其实是:一旦决定启动投票箱,结果必须受到无条件的尊重。而在刚刚摆脱独裁专制的新兴民主社会,也许是公民的合法政治权利长期备受高压所激发起的一种心理补偿的需要,人们恨不得在社会的每一个角落都摆满投票箱。然而,假如开票的结果不是自己的选择,他们就会立刻倾向于把自己经过浴血奋斗建立起来的投票箱一脚踢开。<br />
<br />
<br />
　　在我看来,当红衫军高举着他信的巨幅画像高喊“阿披实下台”的时候,他们等于在宣布自己不承认上一次总理大选,因而实际上也就等于宣布他们反对泰国现行的民主选举制度。但假如真是这样的话,他们又没有提出任何改革现行选举制度的主张。说到底,他们要的只是他信,而不是民主制度―――为了他们拥戴的他信,他们可以轻易地抛弃民主。<br />
<br />
<br />
　　一个人“有权利”做一件事情,并不等于他“有能力”做成这件事情。新兴民主社会民众身上高涨的政治权利意识与幼稚的政治行为能力之间的惊人反差更加集中地表现在,他们不能明智地区分政治与法治的关系,他们无一例外地继承了专制独裁时期政治代替法治的精神传统。他信在任总理期间的贪腐行为是证据确凿的,如果在美国这样法治完备的成熟民主国家,他就算有再高的民意支持率,也难逃遭弹劾下台的命运,但缺乏独立性的泰国司法系统和他信政治势力掌控的泰国议会不可能做到这一点。于是,为了纠正一个政客贪污腐败的错误,就必须通过发动一次军事政变这样一个更大的错误来完成。我们应该能够注意到,在他信的反对者中,除了利益受损的曼谷权贵阶层外,确实有不少理性的民主法治的倡导者和维护者,包括好几任泰国前总理和一些资深议员。遗憾的是,为了扳倒他信这个“贪污犯”和“独裁者”,他们与红衫军一样,也轻易地默认甚至加入了军人干政这样一桩有损民主法治的行动。这当然会进一步激起他信支持者的强烈对立―――还有什么比用武力推翻一个民选政府更不能为民主社会所容忍的?<br />
<br />
<br />
　　(作者系资深媒体人)</div>

							
							
							
							
