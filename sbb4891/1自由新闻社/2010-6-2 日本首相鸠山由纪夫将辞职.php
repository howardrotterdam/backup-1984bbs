													<h2>违背竞选承诺 日本首相鸠山由纪夫宣布辞职</h2>
						
						
																											<div id="postmessage_653260" class="t_msgfont">去年8月30日，日本民主党在大选中获得众院总数480议席中的308席，将执政半世纪的自民党请下台，自民党党首鸠山由纪夫因而当选为第93任日本首相，被普遍期待给日本政坛带来“改变”。9个月后，没有如竞选承诺一样改变驻日美军“普天间机场”地址的鸠山将辞去首相之位。<br />
中新网6月2日消息，根据日本NHK电视台的消息，鸠山由纪夫在2日上午召开的民主党两院议员大会上表示，由于驻日美军普天间机场搬迁问题造成社民退出联合政府等政局混乱局面，自己将引咎辞职。民主党干事长小泽一郎也将辞职。<br />
近几周以来，鸠山的支持率持续下降。在驻日美军普天间机场搬离冲绳问题上，日本国民普遍认为他违背了当初上台时承诺的“县外搬迁”。《每日新闻》民调显示，58%民众认为鸠山未能兑现承诺，应该引咎辞职；有学者更认为，鸠山已成“大话首相”，必须下台。 <br />
据新华网早前消息，日美两国5月28日上午就普天间基地搬迁发表共同声明，决定把这座航空基地迁至冲绳县名护市边野古美军施瓦布军营沿岸及附近海域，建设长1800米的跑道。<br />
鸠山承认冲绳居民多年来饱受美军基地扰民之苦，承认搬迁方案“不完美”，“我没有兑现承诺……我深表歉意”，但政府“别无选择”，只能请求冲绳居民理解。<br />
鸠山说，民主党还是反对党时就主张把普天间基地迁出冲绳甚至日本，但过去5个月内，日方在冲绳县外寻找新址，考察多处地点，但最终得出结论，整个基地即便迁至日本本土“不现实”。他强调美国在冲绳驻军对日本国家安全的重要性。<br />
一直反对基地留在冲绳岛执政联盟中的社民党30日已因此宣布退出鸠山内阁。社民党主张把位于冲绳县宜野湾市的普天间基地迁出冲绳，强烈反对把这座机场迁至边野古地区，认为在那里填海造地将给当地环境造成破坏。<br />
去年9月开始的日本民主党、社民党、国民新党的联合执政就此宣告瓦解。<br />
鸠山内阁近期将全体辞职。在众院占多数的民主党将加紧选出后任党代表，并经首相提名后继续维持政权运营。后任首相候选人中，副首相兼财务相菅直人（63 岁）的呼声较高。<br />
【南方周末】本文网址：<a href="http://www.infzm.com/content/45865" target="_blank">http://www.infzm.com/content/45865</a></div>

							
							
							
							
