【新民网・独家报道】9月7日上午8时45分左右，一男子从自己住所下楼，走在上班路上，突然被一不明身份人士用利器刺伤臀部。受伤男子随后被送往医院救治，但行凶的疑犯事发后逃离现场，目前尚未被找到。<br />
<br />
&nbsp; &nbsp; 事发地点位于西藏中路725弄10号，是一幢名为家化中房苑的商住两用高层楼房。新民网记者在现场了解到，被刺男子大约30岁，事发时身穿一件蓝白相间的上衣正准备上班，在走出家化中房苑小区的大门后，突然被人从背后用利器捅伤臀部。据目击者称，被捅伤男子左边裤腿上有一大滩血迹，被捅后他迅速跑回10号楼内求救，随后物业帮助其报警。<br />
<br />
&nbsp; &nbsp; 物业工作人员高先生告诉记者，被捅男子事发后曾拿着手机，身上带着血跑入物业办公室求救。“他说有人在追他，自己已经不会拨电话了，叫我帮他报警，神情十分慌张。”高先生告诉记者，事发后一位路过的“大奔”司机好心相助，将伤者送往医院救治。<br />
<br />
&nbsp; &nbsp; 家化中房苑的物管人员称，大楼的门口装有监控，事发后监控录像已经被警方提取。新民网记者在现场发现，事发现场就在西藏路桥边上，周边地形相对比较复杂，附近居住的人员也比较杂乱。随后，新民网记者从黄浦警方了解到，捅人者在事发后逃离了现场，目前警方尚未找到疑犯。<br />
<br />
&nbsp; &nbsp; 在上海市长征医院，新民网记者从急诊室当班医生处了解到，被捅男子伤口为利器所伤，伤口较深，宽约1.5厘米，无生命危险。伤者在清创、缝合伤口处理后即可出院。(新民网记者 胡彦 萧君玮)</div>

							
							
							
							
