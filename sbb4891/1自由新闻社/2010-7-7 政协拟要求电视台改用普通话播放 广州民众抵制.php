广州电视台主要频道拟改为普通话播出之所以惹众怒，还有一个原因，皆因该台去年将经济频道和「午间新闻」改为普通话播出，结果收视插水，广告大跌，令台方「头痕」，惟有将「午间新闻」悄悄改回广东话播出。<br />
<br />
耗资3000万　广告插水<br />
<br />
为迎接亚运会，广州电视台去年耗资3,000万元人民币，将原来用广东话播出的经济频道改为普通话播出（对外称广州电视经济台），并以「风生水起，广州经济」为全新呼号，另将粤语播出的「午间新闻」也改为普通话。但改版后的经济频道，收视率从去年4月的0.34个百分点，下降到今年4月0.09个百分点，形同插水；午间普通话新闻收视率也大跌。两档改版节目的广告越来越少，令电视台「头痕」；今年1月，午间新闻便悄悄改回以广东话播出。<br />
<br />
广州电视台创办于1988年，当年为应对香港电视对珠三角影响，国家广电部（现国家广电总局）特批广东珠江频道（现为广东省电视台）及广州频道使用粤语播出节目，其中广州频道即为广州电视台前身。当局声称，广州电视台现时的收视率，已超过香港本港台和翡翠台在当地的收视。<br />
