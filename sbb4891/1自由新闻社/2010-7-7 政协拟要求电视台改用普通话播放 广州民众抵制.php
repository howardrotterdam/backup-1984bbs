--------------------------------------------------------------------------------<br />
电视台增方言频道受欢迎<br />
<br />
广州市政协建议广州电视台由广东话改成普通话播出，引起民众反弹。其实，内地不少电视台正反其道而行，纷纷开设方言频道，虽与国家广电总局方针不符，却增加了当地居民的亲切感，大受欢迎，收视率也不断攀升。<br />
<br />
北京打压沪语节目<br />
<br />
国家广电总局对方言类新闻等节目一直持反对意见，但无阻地区电视台推出方言节目，如山东齐鲁电视台的《拉呱》，济南电视台的《有么说么》，杭州电视台的《阿六头说新闻》，重庆电视台的《雾都夜话》、《生活麻辣烫》等，都是当地招牌节目，深受观众欢迎。<br />
<br />
上海作为中国的国际城市，其广播电视中出现上海方言的节目却不多。有从业者将此归咎于北京打压。早前上海台曾制作沪语版《猫和老鼠》，最终因国家广电总局禁令而不能播出，以致有市民抱怨，「上海的小孩子连上海话都不会说了」！<br />
<br />
有内地学者指，现时新闻竞争越来越激烈，媒体已很难抢到独家新闻，要吸引人就须在解读方式上与别的媒体有区别，在这方面，方言类节目有不可忽视的优势，因贴近民众生活，形式生动活泼，容易引起当地观众共鸣。<br />
