													<h2>政协拟要求电视台改用普通话播放 广州民众抵制</h2>
						
						
																											<div id="postmessage_726210" class="t_msgfont">AppleDaily苹果日报网上版－－2010-07-07<br />
--------------------------------------------------------------------------------<br />
政协拟逼电视台改用普通话播放　民众抵制<br />
广州爆广东话I卫战<br />
<br />
<img src="https://yopic.us/images/a3101b.gif" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
广州电视台女主播王燕（着红衣者）呼吁广州人出来挺粤语。<br />
<br />
广州正掀起一场广东话I卫战。广州市政协正式向当局建议，将广州电视台主要频道由广东话改为普通话播出，以适应亚运会及广州拟打造国际都市形象的需要。但有关建议随即遭民众抵制，并引发网民逼爆政协网站。有持反对意见的政协委员怒轰有关建议属「忘根」，更有电视台主播呼吁广州人要出来挺粤语。<br />
<br />
广州电视台现有综合、新闻、经济、少儿、影视等九个频道，基本用广东话播出。但月前传出消息，指广州市政协准备建议当局，将广州市电视台主要频道改用普通话播出，事件引起民众和舆论关注。<br />
<br />
至前日，市政协正式向当局提交建议，指为营造良好的语言环境，建议把广州电视台综合频道或新闻频道，改为普通话播出，以适应亚运会国内外宾客需要。官员解释，此举也是为广州拟建国际大都市和广州电视台准备上卫星的需要。根据国家有关规定，只有普通话频道才能获批准上卫星。<br />
<br />
网民逼爆政协网「母语告急」<br />
<br />
<img src="https://yopic.us/images/a3101a.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
有关建议随即引起民众的极大回响。广州市政协委员韩志鹏当日下午到政协常委小组讨论会踩场，发炮轰有关建议不当，强调应「增加普通话频道而不是减去广东话频道」；他指，粤语是岭南文化载体，更是一条根，「不能让下代人为粤语申遗，那样太可悲了」。<br />
<br />
韩志鹏还在网上呼吁：「母语告急，岭南文化垂危！」指粤语普通话之争「其实是岭南文化与北方文化的冲突」，指许多在广州工作生活的华侨港澳同胞，大都不会「煲冬瓜」（即「普通话」谐音），当局不应忘了他们对广州建设的贡献；更指市政协早前有关调查近约八成人反对，「为甚么不尊重结果」？<br />
<br />
《广州日报》网版删有关报道<br />
<br />
广州市政协上月曾在官网征求对有关建议的意见，结果反对者达79.5%，同意者仅占20.5%。更重要是，民众反对情绪激烈，一度逼爆网站表达反对声音。广州电视台新闻主播王燕在网上直指：「一种语言代表一种文化，是广州人就要挺身撑粤语！」<br />
<br />
而广州电视台《新闻日日睇》前主播陈扬更呼吁，市民要挺身I卫粤语，形容「粤语正在沦陷」，指「被消失的方言后，必定是被弱势化的文化……今天广州人的母语被赶走，明天你的母语也不会安宁」。有网民质问：「广州有2224年历史，粤语作为核心文化，凭甚么不能成为电视播出语？」但也有网民认为，广州应有开放包容胸襟，「让外来人融入广州，首先是语言融合」。<br />
<br />
据悉，广州市长万庆良收下政协的建议书，但无表态，仅指示下属「研究」。有读者注意到，《广州日报》昨有刊登相关报道，但网上电子版却「开天窗」，显示广州当局不想有关报道影响扩大。<br />
<br />
