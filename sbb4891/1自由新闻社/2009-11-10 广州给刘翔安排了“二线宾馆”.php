													<h2>广州给刘翔安排了“二线宾馆”</h2>
						
						
																											<div id="postmessage_321025" class="t_msgfont">再火爆的事物也会让外界产生“审美疲劳”，更何况广州是一座非常务实的城市。<br />
<br />
将在明天参加田径亚锦赛的刘翔与师父一行昨天抵达广州，与外界的预料相反，济南的“刘翔效应”并没有复制到广州。落地后的翔飞人几乎是在无人搭理的情况下悄然离去。而广州组委会对刘翔也没有想象的特殊DD没有传说中的300记者和5000保安。就连给刘翔安排的酒店，据说也只是三四星级的“二线宾馆”。<br />
<br />
当刘翔在下午5点抵达广州新白云机场时，传说中的300记者最终只剩约20名记者接机。一位现场蹲守的广州同行告诉记者：“刘翔出来的时候，没有一名记者上去采访他。因为大家都知道，反正他也不会说什么。只有摄影记者凑上去拍了几张，刘翔上了车，大家也都散了。”和过去不一样的是，在广州机场刘翔一行人遭遇了冷场：不仅没有引发“粉丝”追逐，甚至连找刘翔签名的人都没有。“我们也有些意外，刘翔出来的时候，和他同机出来的乘客一点反应都没有，以前采访刘翔时，都会看见贵宾通道的工作人员争着找他签名。但广州机场的工作人员仿佛不认识刘翔一样。”一位蹲守的外地记者感叹。他随后也带着这个疑问采访了一位工作人员，对方的答案很有意思：“不就是刘翔吗？其实，我们机场的贵宾通道工作一直都很繁忙，每天都要解送不少的国内外重要人物，刘翔来的确算不上大场面。”<br />
<br />
在毫无“阻拦”的情况下，刘翔就坐上了开往下榻酒店的商务车。而针对刘翔的到来，广州组委会也并不想费周折，一位工作人员告诉记者：“刘翔的待遇跟其他运动员一样，我们不会为他搞特殊。” 据了解，本次参加亚锦赛的运动员被安排分散住在了几个酒店，尽管无人知道刘翔前往何处，但组委会证实所有下榻酒店都只有三四星级水平的“二线酒店”，“即使是刘翔也不会为他安排顶级酒店。”此外组委会也不打算专门为刘翔开辟适应场地的“特殊时段”。一位工作人员透露：“现在还不清楚刘翔的训练时间安排，但除非明天白天有统一安排，否则刘翔不会有适应场地的机会。”<br />
<br />
<br />
此前有媒体传出，本次广州组委会将效仿十一运组委会，投入5000安保力量为刘翔护驾。但这一消息昨天被否定。据了解，尽管本次广州赛场也将投入大量的安保力量，但主要保护的对象却是现场观众。“我们首要的任务是保障观众的安全和现场的秩序，没有接到专门针对刘翔的安保通知。”到目前为止，本次田径亚锦赛接受的预订票只有数千张，至于临场票房会否好转，相关人士并不看好。“主要是赛场离市区太远了，就算刘翔来了，按照广州人的习惯，也不太愿意跑这么远看比赛。”<br />
