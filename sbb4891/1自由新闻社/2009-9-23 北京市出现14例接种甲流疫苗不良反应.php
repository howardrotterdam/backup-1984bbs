<br />
　中新网9月23日电 “总体来说，国产甲流疫苗接种非常安全，特别是不会致死”，在今天卫生部新闻发布会上，中国CDC免疫中心主任梁晓峰在解释接种甲流疫苗的不良反应时表示，“如果诊断鉴定严重不良反应，省级财政还有经济补偿”。 <br />
 <br />
　　梁晓峰说，经过批准上市的疫苗，首先必须是安全的，同时，不良反应也肯定会有。局部的不良反应主要是疼痛、红斑和硬结，全身性的包括发热，甚至是过敏也是会出现的。这些一般在接种后12小时以内发生，可能会持续1-2天。目前，中国掌握的不良反应情况有高有低，常见大概是1%-10%的不良反应率，多是局部反应，异常或者严重反应非常罕见。<br />
<br />
　　对于为什么接种疫苗以后会发生严重的病例或者死亡，梁晓峰解释说，首先可以肯定，死亡都是耦合的，有些是加重本身的基础病进程所以造成的。因为，即便不接种疫苗，有些慢性病也可能发生死亡，所以接种以后偶发的一些死亡肯定是有的。<br />
<br />
　　因此，有基础性疾病的人接种，就是一个权衡利弊的问题。一方面有基础性疾病的，特别是急性期的不能接种，因为怕发生一些耦合性反应，加重疾病；另外呼吸道和慢性疾病是保护对象，一旦感染新疾病后会加重病情，因此必须要认真考虑每个人的情况。<br />
<br />
　　梁晓峰强调，假设大面积、大范围接种以后，出现三级反应很高，甚至达到四级住院情况，就需要考虑叫停。因为疫苗的免疫接种要遵守有利于的原则，权衡利弊。<br />
<br />
　　梁晓峰表示，除了紧急叫停机制，这次接种还非常注重与受众、监护人的沟通。把疫苗的有效性和安全性讲清楚，并且认真做好每个个体的交流，经过受众同意才能实施。现在，国家疾控中心不良反应监测系统和药监局的AEFI已经密切配合，希望能通过网络捕捉或者接收到基层报告的不良反应。 <br />
<br />
<br />
<br />
　　北京晚报讯 （记者贾晓宏）北京市昨天（21日）率先启动甲型H1N1流感接种工作。市卫生局有关负责人介绍，本市已经订下500万支甲型H1N1流感疫苗，按照不同人群疫苗接种的优先次序，将优先对参与国庆庆典活动的学生开展接种。 <br />
 <br />
十一长假后，将为易感人群接种，但具体的接种范围及接种方法等需根据流感疫情的暴发情况酌情而定。<br />
<br />
　　市卫生局专门从全市三级医院抽调了500名技术精湛、经验丰富的医护人员，组成49支甲流疫苗预防接种小分队，赴大、中、小学校开展预防接种工作。昨天，卫生部门除在学校开始接种甲流疫苗外，公交集团负责国庆保障的司机也将陆续接种甲流疫苗。市卫生部门表示，全部国庆演练及保障人员的接种工作在本周五前完成。<br />
<br />
　据了解，目前全市约有7万中小学生参与国庆庆典和群众游行。根据市教委的通知，北京市中小学生的接种分为两个阶段进行，其中，9月21日至24日，优先为国庆庆典中小学生开展接种；10月8日至10月30日，为全市其他中小学生开展接种。<br />
<br />
　截止22日17点，北京市累计接种甲流疫苗39000人，其中，共发生14例不良反应。中国CDC免疫中心主任梁晓峰23日在卫生部新闻办公室、中国疾病预防控制中心23日举行秋季流感防治媒体通气会表示。<br />
<br />
　　梁晓峰表示，截止到22日17点，北京市当天共有28864人接种甲流疫苗，累计接种39000人。 <br />
