<br />
&nbsp; &nbsp; 现在距离国庆只有7天。据北京媒体披露，在这7天中，有关部门要给参加国庆庆典的所有7万名中小学生接种甲流疫苗。笔者认为要慎重。鉴于甲流疫苗属于新药，未经时间检验，一旦这7万名国庆中小学生参演者接种后出现集体反应（经前期实验，部分接种甲流疫苗者出现皮肤红肿和低烧），后果不堪设想。<br />
