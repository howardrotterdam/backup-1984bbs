													<h2>飞虎队老兵垂危无钱医治 曾64次驾驶战机重创日寇</h2>
						
						
																											<div id="postmessage_756841" class="t_msgfont">飞虎队老兵垂危无钱治 曾64次驾驶战机重创日寇<br />
 来源：法制晚报 2010年07月24日 <br />
彭老举手向记者示意，但身体虚弱，动起来十分困难　摄/记者郭谦 <br />
<br />
<br />
　　无钱治病飞虎队老兵生命垂危<br />
<br />
　　为北京唯一健在者 曾64次驾驶战机重创日寇 十天前白血病复发入院老伴决定卖房凑钱<br />
<br />
　　他曾64次驾驶战机重创日寇，被授予“优异飞行十字勋章”，如今他只能躺在病床上回忆当年的浴血奋战；他曾视死如归，带着共有20多个弹孔的战机返航，现在他却在病魔面前无奈地选择放弃生命。<br />
<br />
　　彭嘉衡老人，北京唯一健在的飞虎队队员，近日因白血病复发再次住院，病情不容乐观。<br />
<br />
　　病房探视<br />
<br />
　　手指撑开眼睛坚持看清探视者<br />
<br />
　　昨天，解放军307医院血液科病房中，近90岁高龄的彭嘉衡老人虚弱地躺在病床上闭目养神。<br />
<br />
　　病情复发后，身高1米83的彭老体重从70多公斤锐减到60多公斤，整个人干干瘦瘦，失去了往日的矍铄。<br />
<br />
　　因为病痛，彭老每隔几分钟就要皱着眉头稍稍变换一下姿势，就连这个简单的动作都需要家属协助完成。<br />
<br />
　　尽管如此，当听到小儿子彭灼南说《法制晚报》记者前来探视时，彭老一下子呼吸急促起来。<br />
<br />
　　他用力举起没有扎点滴的右手，颤巍巍地用食指和拇指撑开右眼，执意要看清记者的模样。<br />
<br />
　　凭着听觉，他努力将脸转向记者的方位，口齿不清但一字一顿地说道，“我记得《法制晚报》，去年采访过我，感谢你们对我的帮助。”<br />
<br />
　　病痛折磨<br />
<br />
　　接受骨髓移植无奈白血病复发<br />
<br />
　　去年12月，本报记者采访时，彭老刚刚接受了小儿子彭灼南捐献的骨髓，进行了移植手术。<br />
<br />
　　“手术后效果还不错，今年3、4月份是老爷子身体最好的时候。”彭灼南回忆，春天时，老爷子还能拄着拐四处散步。<br />
<br />
　　一个月前，彭老外出参加活动，旁边的人劝年迈的彭老坐下，可彭老坚持要站起来以示尊重，众人连忙将拐杖递到彭老手中，彭老却悄悄地将拐杖藏了起来，坚称自己就是一个“能走能站不用人扶”的健康老头儿。<br />
<br />
　　就在大家都以为彭老的身体逐渐好转时，10天前，彭老突然感觉不适，将刚吃下去的饭菜吐出后就病倒了――他的白血病复发了，整个人虚弱不堪。<br />
<br />
　　之前的治疗已经让彭老家中一贫如洗，如今，面对着高额的医疗费，家人一筹莫展。<br />
<br />
　　放弃治疗<br />
<br />
　　已花费十多万老伴卖房凑钱<br />
<br />
　　从战争年代的出生入死到和平年代的平淡生活，彭老从来没有怕过什么，更不曾抱怨，不管贫富老两口都幸福淡然，安之若素。而今，他却在病重时向妻子透露自己的决定――放弃治疗。<br />
<br />
　　“四天的医药费就要一万元左右。现在他只能靠药物来维持，如果断掉连十多天都坚持不了。”老伴说。<br />
<br />
　　彭老从民航局退休后，民航局曾在他做移植手术时为他报销过一部分医药费，但由于彭老档案的原因，民航局无法按飞行员的待遇报销其医药费。老两口已经花去了10多万元，经济拮据。<br />
<br />
　　“打仗的时候他连死都不怕，现在却要放弃自己的生命，我觉得很对不起他。”话一出口，彭夫人抓着胸口的衣服泣不成声。彭老倒下后，这个柔弱的女人一下子坚强起来，陪伴在丈夫身边。<br />
<br />
　　“他闹着要回家，不住医院，我知道他心疼我，心疼钱。”彭夫人抽泣着，“现在他只能靠药物来维持，我已经决定把家里的房子先卖掉，抵上医药费再说。”<br />
<br />
　　今天上午，彭夫人拿着辗转借来的5万元赶到医院，“不知道他还能坚持多久，但不到最后我不会放弃，我不想看到他走得太痛苦。”<br />
<br />
　　人物背景<br />
<br />
　　彭嘉衡，1921年7月生于印尼，1938年参加国民革命军第4集团军交通兵团，1942年赴美国高级飞行学校学习。1944年加入飞虎队。<br />
<br />
　　1947年，彭嘉衡退役返回印尼。1950年回国进入民航局工作，1986年退休。因参加对日空战超过50次，1945年获美国空军颁发的优异飞行十字勋章。<br />
<br />
　　飞虎队背景<br />
<br />
　　飞虎队全称为“中国空军美国志愿援华航空队”，1941年7月来中国参与抗日，后被编入美国第14航空队。<br />
<br />
　　资料显示，至抗日战争结束，第14航空队共击落日敌机2600架，自身也有重大损失。<br />
<br />
　　文/记者王t王贺健<br />
