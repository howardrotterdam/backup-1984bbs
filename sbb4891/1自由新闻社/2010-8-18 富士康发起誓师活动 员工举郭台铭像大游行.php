																											<div id="postmessage_787300" class="t_msgfont">来源：中国新闻网<br />
<br />
中新网8月18日电 据台湾《中国时报》报道，台湾鸿海集团昨日表示，由富士康工会组织在大陆各厂区发起的“珍惜生命，关爱家人”誓师活动，十八日傍晚五时将在深圳厂区大规模举办，且将第二次对媒体开放。<br />
　　富士康接连发生员工坠楼事件后，郭台铭即于短时间内要求大陆各厂区设置天网、地网和爱心防护的隐形“心网”。<br />
　　报道说，富士康工会为此发起誓师活动，希望员工之间无形的“爱心网”能取代防护网，该活动并从富士康太原厂区率先作起。<br />
　　鸿海表示，太原厂已在十六日进行万人誓师，并拆除先前设置的安全防护网。不过，除了太原厂区以外，包括深圳在内的大陆其它厂区，誓师后并不会马上拆除防护网。<br />
　　据悉，誓师活动主要是宣读由工会起草的倡议书，呼吁员工通过正当途径表达合理诉求，也就是“工作困难找公司，生活困难找工会”，希望透过完整的体制，在有形无形中，协助员工实践“安全生产、健康生活”等目标。<br />
　　报道说，以规模来看，今天的深圳厂誓师，将是鸿海有史以来最大型的员工自发性活动，预计由深圳十多个厂区联机举行，台北的土城总部也会以视讯实况转播。<br />
　　此外，为了让外界了解富士康情形，富士康深圳厂十八日也将第二次对媒体开放。<br />
　　不过，鸿海集团董事长郭台铭目前并未安排亲自出席誓师大会，但鸿海内部称也不排除郭台铭现身的可能性。</div>

							
							
							
							
