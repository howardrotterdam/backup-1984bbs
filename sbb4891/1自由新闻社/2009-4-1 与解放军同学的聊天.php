・德国： <br />
1982年10月13日，中、德两国政府签署了技术合作总协定，开始对华提供无偿援助。 <br />
<br />
截止2006年7月，德国政府共向我提供无偿援助7.740亿欧元，其中技术合作资金5.555亿欧元，人员培训8500万欧元（每年500万欧元），提供紧急救灾援款4250万欧元，提供“营养保障”粮援资金9100万欧元。 <br />
