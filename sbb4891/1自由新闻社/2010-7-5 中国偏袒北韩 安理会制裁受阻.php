<br />
联合国代表团的一位外交消息人士1日表示，中国对安理会如何应对的问题具有重大影响力，而中方却在安理会谈判桌上“露骨地偏袒北韩”，使文案的制定面临困难，该消息人士还透露谈判氛围说：“比想象的更加艰难。”“事件”一词是北韩常驻联合国代表申善浩上月15日召开记者会时一直使用的词语。据悉，如果韩方对此提出强烈异议，中方就会改变文章脉络，调整字句并降低力度，展开“拉锯战”。<br />
天安舰爆炸沉没事件正式被提交安理会已经一个月之久，在有关事故原因的南、北韩记者会上，韩国也获得绝对支持，但强烈谴责北韩的联合国安理会措施至今尚未出炉。韩、美、日三国认为，天安舰爆炸沉没事件明确是违反《停战协定》的挑衅行为，应该写明北韩的责任并要求北韩进行高水平的道歉和承诺防止类似事件再次发生。而中国和俄罗斯则主张，很难力证北韩有直接责任。双方的立场针锋相对。<br />
<br />
由此看来，即便是安理会戏剧性达成妥协，发表主席声明，其力度也可能会低于八国集团（G8）声明。韩国常驻联合国代表团的一位有关人士表示：“八国集团中只有俄罗斯袒护北韩，因此可以通过力度相对较高的声明，但在安理会中，身为常任理事国的中国和俄罗斯都积极袒护北韩，性质不同。”<br />
<br />
八国集团领导人上月27日曾发表联合声明称：“对导致天安舰沉没并造成46名韩国海军官兵牺牲的攻击行为表示遗憾。韩国军民联合调查组的调查结果表明北韩对该事件负有责任，对引发天安舰事故的攻击行为提出谴责。”<br />
<br />
联合国内外分析称，中国袒护北韩是因为担心在北韩强烈抗议的情况下，如果参与对北谴责行列，可能会导致韩半岛局势恶化。据分析，就中国而言，不仅要考虑到北韩，还要考虑到同韩国之间的关系，因此，中国采取的战略是，赞同安理会的整体立场，但也会最大限度地降低力度，反映北韩立场。<br />
<br />
韩国政府一方面向中国施压，要求中国发挥符合其国际地位的负责任的作用，一方面反复强调“国际社会必须采取措施，以免北韩产生无论做什么事都不用付出代价的认识。”韩国常驻联合国代表团的一位有关人士表示：“目前的状况既不乐观也不悲观。谈判不会超过下周，不排除戏剧性达成妥协的可能性。”<br />
<br />
