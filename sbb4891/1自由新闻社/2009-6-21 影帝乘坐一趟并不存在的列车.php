<br />
　　2008年1月2日、3日，哈尔滨至北京间加开D18次列车，哈尔滨开车时间为8时57分，到达北京时间为16时52分。该车票价与动车组D26票价相同，开行当日到达北京。（刘海楼 本报记者 王冠群）<br />
