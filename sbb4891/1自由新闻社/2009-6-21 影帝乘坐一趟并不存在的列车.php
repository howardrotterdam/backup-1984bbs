<br />
　　从2007年12月29日开始，哈尔滨火车站迎来了元旦客流高峰，当日客流达到12.6万人，比平时客流增加2万余人。2007年12月30 日，哈站客流再创新高，达到14.4万人，比2007年“五一”黄金周日均客流还高出1.2万人。元旦期间大部分客流主要集中在省内齐齐哈尔、佳木斯等线路，省外客流北京方向最为集中。针对这种情况，铁路部门开通4对临客列车确保市民出行。<br />
