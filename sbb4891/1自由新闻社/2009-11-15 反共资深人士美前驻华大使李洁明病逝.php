俞强声是中共元老黄敬之子，乃现任中央政治局委员丶上海市委书记俞正声（64岁）的兄长。他的叛逃一度对俞正声仕途造成影响，但俞正声仗着与邓小平儿子邓朴方的关系避开锋头，从北京到山东青岛任职，直至 20年後才东山再起，主掌上海，并进入中共政治局。<br />
<br />
中国情报官俞强声叛逃事件轰动世界，但20多年来，中美两国官方及情报界对此事均无任何表态。李洁明在生前出版的回忆录，亦只提及金无怠间谍案。<br />
<br />
加拿大前特工作家霍夫曼（Tod Hoffman）曾出版《内部间谍──金无怠和中国对中情局的渗透》一书，书中有一章关於中国间谍「飞机人」（Plane Man，CIA为俞强声取的代号）的精采描述，美国特工与他秘密接头联系，以及最後被策反的过程，直指这是李洁明和他手下的「杰作」。<br />
<br />
由於俞强声叛逃，1983年才成立的中国安全部被迫脱胎换骨大改组，部长凌云被解职，多名相关人士被处分。俞强声逃美後，自此下落不明。有传他已整容，也有说他逃美两年後，被中国情报人员溺毙。<br />
本报记者 <br />
<br />
<strong>不屑中共侵人权　支持两岸民主</strong><br />
2009年11月15日 <br />
<br />
李洁明一生从事对华谍报工作，了解中共历史，不屑中共反文明作为，使他成为一名坚定的反共政治家。但他积极支持台湾的民主发展，并於 1995年促成时任台湾总统李登辉访美，触怒中共，令中美关系一度紧张。<br />
<br />
<strong>促成李登辉访美</strong><br />
<br />
李洁明在回忆录中称，自己虽然在中国出生，但中文却只有四岁小孩子的水平，只会用来打招呼丶骂人和说大小便。但谦虚并不妨碍这位政治家对中国的深入了解，特别是文化大革命以及1989年六四事件，使他对中共有深刻认识，坚定其反共立场。<br />
<br />
<strong>六四事件中，中共指摘美国 CIA介入了中国学运，并将账记在李洁明头上。</strong> 1991年 5月李结束任期回国，此後再无缘踏入中国大陆。但这不妨碍他对中国民主的支持。他曾两度接受台湾邀请赴台访问，并多次公开演讲赞赏台湾的民主，批评中共侵犯人权。<br />
<br />
晚年，李洁明在美国马里兰大学（ University of Maryland）办了一个东亚问题研究中心，获台湾方面捐款。 1995年在他斡旋下，美国批准被中共指搞台独活动丶时任台湾总统李登辉访美，事件令中共大怒，一度以向台湾以东海域射导弹，及在沿海军演作威胁，中美关系一度倒退。<br />
<br />
本报记者 <br />
<br />
<strong>两度驻港收情报　怨港特工为钱</strong><br />
2009年11月15日 <br />
<br />
李洁明曾两度在香港进行对华情报工作，一次是上世纪 50年代初在香港大学，以学生身份从事谍报工作，另一次是 60年代初期被美国中情局（CIA）派驻香港。他在回忆录中曾抱怨，在香港特工一味只想发财，将情报费中饱私囊，令他生气又无可奈何。<br />
<br />
<strong>指中情局做法食古不化</strong><br />
<br />
1953年 5月，李洁明以学生身份入读香港大学，以读书为名，收集中国大陆情报为实，并以「约翰．赖特」（ John Right）名字活跃於社交场合。当时港英当局不希望美国用香港搞间谍活动，他的活动因此受限。某次，他旗下的一名特工因涉走私武器被查获，他因担心自己身份暴露，只好离港回国。 60年代初，他再度赴港，是中情局驻香港第二号人物。他在回忆录披露，当时中国大饥荒，他的手下收到解放军总参谋部编印的工作通讯影印本，其中有描写解放军毒打盗窃食物老百姓的具体报道，对美国了解中国的大饥荒及军方工作很有帮助。<br />
<br />
<img src="http://us.f6.yahoofs.com/hkblog/4KYsljeZFRsQdQNJYxPdzEQ_s0QFrvi__DOT__yqSZuXU-_2/blog/20091115122737789.jpg?ib_____DcVpVr7rV" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
被指是李明手下的中共h史＜宜抉R璐（右）c夫人戈P。<br />
<br />
李洁明在回忆录指，中情局在香港的做法食古不化，总是找人带电台到大陆去联系，结果总是「骨头打狗丶有去无回」；他还提到香港的特务一味只想发财，把情报费中饱私囊。据悉，李洁明在港时的手下，包括早年曾参与中共革命的司马璐（着有《中共历史见证─司马璐回忆录》，现居美国， 2002年与流亡美国的中国异见人士戈扬结婚）丶曾在旺角经营书店的李金晔和已故学者许冠三等。<br />
