<br />
李洁明出生於中国青岛，他的父亲是美孚（Mobile）石油驻中国代表。他於 1928年 1月 15日出生时，青岛正被日本人霸占，在青岛 8年，他目睹中国人受欺凌。他在回忆录《中国通：亚洲冒险丶间谍与外交生涯 90年》（ China Hands： Nine Decades of Adventure， Espionage， and Diplomacy in Asia）中亦有提及。<br />
<br />
