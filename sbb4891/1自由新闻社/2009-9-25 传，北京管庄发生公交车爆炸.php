<br />
5点45分左右，管庄站内已解除封锁。站内一名治安员称，刚刚确实出现一些情况，但已经处理完毕。记者在出站口附近看到，3名特警携带两只搜爆犬正在待命，但10多分钟后人犬全部撤离，站内外完全恢复正常。 (本文来源：京华时报 )虫虫</div>

							
							
							
							
