时间：3月24日上午<br />
<br />
地点：驻香港部队深圳基地教导团<br />
<br />
仙村训练场<br />
<br />
经过：<br />
<br />
A 新兵进行入伍后首次实弹练习。一名新兵投掷手榴弹时意外脱手，撞到掩体墙面反弹回来。<br />
<br />
B 千钧一发时刻，身边的队长伸手拽过新兵，拖到隔壁掩体，并伏在新兵身上。<br />
<br />
C 手榴弹爆炸，两人毫发无损。<br />
<br />
文/记者黄蓉芳 通讯员郑威、肖小林、王五开 <br />
<br />
3.5秒，是打响一个喷嚏的时间；3.5秒，也是一枚手榴弹从引弹到爆炸的时间。近日，在驻香港部队深圳基地教导团仙村训练场，就是在这短短的3.5秒里，发生了一场死与生之间的逆转：一位指挥员“弹下救人”，把一名投弹失败的新兵从生死线上拉了回来。<br />
<br />
现场回放：<br />
<br />
手榴弹砸到侧墙反弹回脚边<br />
<br />
昨日，通过实拍录像，记者目睹了当时惊心动魄的整个过程。<br />
<br />
3月24日上午10时，驻香港部队深圳基地教导团仙村训练场上，150多名海、空军新战士正在这里进行手榴弹实投作业，进行入伍以来的第一次实弹投掷。<br />
<br />
11时40分，轮到二营新兵训练队新战士满孝光上场了。他手握一枚手榴弹，迈着正步走进掩体，在投弹处立定。站在他左侧掩体里的指挥员是他的队长黄立明。<br />
<br />
“准备手榴弹！”黄立明下达口令。<br />
<br />
捅破防潮纸――套上拉火环――握紧手榴弹，平生第一次手握真手榴弹的满孝光开始引弹。<br />
<br />
“投！”随着黄立明一声令下，满孝光把手榴弹投了出去。可是，让人目瞪口呆的一幕出现了：已经引弹的手榴弹并没有向远处飞去，而是砸向他右侧的墙壁，然后又反弹到了他的脚旁。<br />
<br />
千钧一发之际，站在满孝光左侧的黄立明一个箭步冲上去，一把拽过他，再往安全区域一扑，把他压在了身下。就在这时，手榴弹“轰”的一声爆炸了。而黄立明一连串的救人动作，一气呵成，速度之快，令人惊叹。<br />
<br />
滚滚浓烟中，两人从地上爬了起来，奇迹般地，毫发无损。<br />
<br />
英勇队长：<br />
<br />
指导新兵投弹 不看人只看弹<br />
<br />
“说实话，当时我根本没时间多想。”昨日，队长黄立明接受采访说起当时壮举，只是简单地解释说“那是一种下意识的本能”。“而且，让我担任指挥员，我肯定要把战士的生命安全放在第一位。”<br />
<br />
“我在指导新兵投弹时，都是不看人，只看弹。”黄立明回忆道，当时，他一直在盯着满孝光的动作，弹一出手，他就看到手榴弹弹到了侧墙上。“如果我只看人，或者稍不注意，就不会那么快做出判断。”<br />
<br />
他说，他之所以能在那么短的时间内做出反应，还因为他有一种与众不同的带兵方式。那就是当战士引弹右脚往后侧移一步时，他也会跟着移一步，那样就能够保持跟战士的距离。<br />
<br />
黄立明说，部队在这种高危险的实弹训练中，都会提前两天进行模拟演练。“以前也演练过类似新战士太紧张，手榴弹扔不出去的情况，但训练中一次也没有遇到过。”<br />
<br />
实为壮举<br />
<br />
黄立明勇救战友的第二天，驻香港部队深圳基地教导团党委便作出决定：号召全团官兵向他学习，并向上级党委申请给黄立明记功。<br />
<br />
“一枚手榴弹从引弹到爆炸，一般只有3到4秒。”团长潘浩明说，在这种情况下要“弹下救人”，多半会受伤甚至会牺牲。“黄立明在这种危急时刻挺身救战友，实为壮举。”<br />
<br />
我在指导新兵投弹时，都是不看人，只看弹。弹一出手，我就看到手榴弹弹到了侧墙上。如果我只看人或稍不注意，就不会那么快做出判断。<br />
<br />
新兵紧张到出汗：<br />
<br />
压根不知道弹没投出去<br />
<br />
“那一下子，我被吓蒙了！”被救的满孝光回忆起当时情景，满心感激。<br />
<br />
满孝光说，那天是他第一次投实弹，心里很紧张。“排队等候时，听到前面战友投弹后的爆炸声，已经手心出汗了。”轮到他时，因为握得太紧，引弹时又用力过猛，手榴弹就向后滑脱出手了。<br />
<br />
“我一点也不知道手榴弹没投出去。投完后还按正常姿势往下一蹲。”满孝光现在回忆起来仍心有余悸，他说，蹲下后，才听到手榴弹掉在“坑里”的声音。“我的脑子一片空白。那一下子，我被吓蒙了！”直到队长把他拽过去扑倒并压在身下，他都没反应过来。<br />
<br />
把他拽过来没感觉什么重量<br />
<br />
那一刻为什么有那么大的力量将身高1.7米的满孝光拽过来扑倒？黄立明解释道：“可能在危急时刻，人的潜能会更大程度地激发。”当然，这也跟平时的训练有关，再加上自己1.8米的个头，他觉得当时把满孝光拽过来“没感觉到什么重量”。<br />
<br />
黄立明：带新兵要有耐心<br />
<br />
今年30岁的黄立明来自湖北，入伍已近12年。他是军事训练标兵，多次被评为优秀基层干部，还荣立过三等功。<br />
<br />
说起他荣立的三等功也跟新兵训练有关。去年初，他带新兵集训，参加驻香港部队的新兵尖子连比武，获得了全驻军的第二名，深圳基地的第一名。他也因此立了三等功。<br />
<br />
“带新兵，不仅是在军事训练上，在生活上，也需要一点一点地带他们。”他说，新兵大多只有18岁，以前在家里没怎么干过活，“从洗衣服、衣领、鞋袜到修整头发，都需要教他们。”<br />
<br />
黄立明至今不敢将救人的事告诉父母。“怕他们知道后担心。”<br />
<br />
满孝光：他像大哥哥<br />
<br />
在满孝光的印象中，队长黄立明高大、威武，在训练场很严肃认真，在生活中又像个大哥哥。“有一次跑3000米，跑完好累，队长竟给我打来了一盆热水泡脚！”他说，连队里很多人都享受过队长这种“礼遇”。满孝光还说，他第二天就打电话把获救的事告诉了父母，“妈妈听后又怕又惊又喜，要我一定要好好感谢队长的救命之恩。”<br />
<br />
