　　《韩战初期志愿军的战术与轻兵器》<br />
<br />
　　本文参考资料：<br />
<br />
　　S.L.A. Marshall:&nbsp;&nbsp;「中共部队的攻击战斗」两篇报告, 1950 年 12 月到 1951 年 1 月间对志愿军的战术分析 <br />
　　美军在1951年八月的报告:敌人拥有的武器 <br />
　　中国近代兵器工业 C 清末至民国的兵器工业(国防工业出版社，北京) <br />
　　抗战时期重庆的兵器工业，重庆出版社 <br />
　　火器堂网站及堂主大作：抗战时期国军轻兵器手册 <br />
　　韩战英文网站：<a href="http://www.rt66.com/~korteng/SmallArms/contents.htm" target="_blank">http://www.rt66.com/~korteng/SmallArms/contents.htm</a><br />
<br />
　　………………<br />
<br />
　　第一部份：志愿军战术<br />
<br />
　　1. 手榴弹是最重要的兵器！<br />
<br />
　　(由参考资料一)在一个布局良好的地区性战斗中，敌军似乎时常喜欢以手榴弹兵为先锋，有时候这些士兵除了手榴弹外并不携带其他任何武器。手榴弹会用绑在腰间的一个布袋装著。他们大量使用手榴弹，当手榴弹耗尽时，我们士兵会看到他们似乎是空著手向我们阵线前进。在手榴弹攻击时，极少有步枪火力的支援，但有一大堆冲锋枪兵夹在手榴弹兵中前进。在更后方，有一到三挺机枪(但在攻击连阵地时不会超过此数)扫射防御阵地的高处和其他地区。就比例来看，我们最严重的伤亡看来是由准确的机枪火力造成的。在以步枪兵打头阵的少数攻击中，他们在接近手榴弹距离时会让手榴弹兵快速地上前穿过步枪兵行伍，以便使用手榴弹。不是所有的手榴弹兵都有步枪，但是大部份步枪兵都带了至少 5 枚小型木杆震撼式手榴弹(译按：原文是 small potato-masher type concussion grenades)。(这些中共士兵都没有刺刀)他们所有的近战战术似乎都在依赖使用手榴弹来迫使联军部队退出阵地。<br />
<br />
　　虽然中共士兵被教育说美军士兵特别无法承受手榴弹攻击，但实际上手榴弹攻击对联军部队的效果不彰。我们许多受到中共军持续数小时手榴弹攻击的兵士仍然对他们的手榴弹表示轻蔑。主要有两个因素造成他们这种态度：<br />
<br />
　　1.&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;中共士兵投掷手榴弹不远且不准确。 <br />
<br />
　　2.&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;中共手榴弹本身杀伤力不强。 <br />
<br />
　　关於第一点，在仔细研究一堆手榴弹战斗的案例后似乎找不出特别的例外。中共士兵使用低手甩动的方式投掷手榴弹，在平地上最远距离不会超过 20 至 25 码，在陡峭的山坡向上投掷时距离更短。在一次山头战斗中，我方部队据守山顶，中共部队在下方 20 码处，他们的手榴弹兵花了一小时尝试将手榴弹投过我们的壕壁，都没有成功。在另外一次战斗中，我方有 7 个士兵紧围著一个小土堆防守，在一个半小时内，中共军於 15 码的距离外对他们投了 50 到 60 颗手榴弹，其中 30 到 40 颗被我们的人踢掉或丢回去，剩下的在他们防御位置中爆炸。这 7 个士兵个个都被手榴弹破片所伤，但都只是皮肉之伤，没有人重伤或阵亡。在另一次排战斗中，有 30 人受到手榴弹破片的伤害，但没有人严重到无法继续进行战斗。因为中共手榴弹而受伤的案例在这些连中比比皆是，但是致命的案例少之又少。似乎只有当身体或头部跟爆炸的手榴弹几乎直接接触时，才会引起重大伤害。我们部队已经习於中共手榴弹的效果，也因此它们不会有损我们的士气。<br />
<br />
　　当在防御中我方的手榴弹供应充足时，中共军的手榴弹兵不是被消灭就是被击退。不过在 Kunu-ri 的战斗中这很少发生。我们的士兵很少配备超过两颗手榴弹，有些步兵根本没有手榴弹。在许多案例中，战斗开始后要再补给(手榴弹)不是完全不可能就是极度困难。但是当手榴弹可以充分供应，而且防御者能有效地使用时，都可以挡住敌人的攻击。<br />
<br />
　　这里Marshall生动的描述了几件事<br />
<br />
　　1.&nbsp; &nbsp;&nbsp; &nbsp;志愿军攻击时，并不是每个士兵都有枪枝。而是以手榴弹兵为前导。有时夹了冲锋枪兵。<br />
<br />
　　2.&nbsp; &nbsp;&nbsp; &nbsp;联军对志愿军所用的手榴弹并不惧怕。因为杀伤力有限，而且投掷距离短，不超过20-25码。<br />
<br />
　　3.&nbsp; &nbsp;&nbsp; &nbsp;虽然志愿军逼近做近战，但是装备里没有刺刀。<br />
<br />
　　志愿军使用的手榴弹，最可能的是沿袭抗战时所用的手榴弹。依照「 中国近代兵器工业 」中国使用的手榴弹是仿造德国的1924式木柄手榴弹。但是因材料缺乏，抗战时火药是把TNT与硝酸钾混合，装药量降低，钢皮弹壳改成铸铁。在内战时期由44厂出了36年式增大破片效果；80工厂在1949年出了38年式，木柄减短。如果联军认为志愿军使用的手榴弹威力过低，抗战及内战时使用的手榴弹大概也好不到那Y。<br />
<br />
　　………………………………………………………………………………………………………………<br />
<br />
　　手榴弹：<br />
<br />
　　德国24式手榴弹，装了TNT120-130课，弹壳用钢皮冲成。巩县兵工厂仿制时，外型和发火装置维持一样，但是钢皮与TNT中国不能制造，改为铸铁。装药改为TNT与硝酸钾(40%+60%)混合70-100克，成为「防卫式」手榴弹。也就是爆发/杀伤力都比德国原型要低。<br />
<br />
　　抗战时有多种变型以减低材料的消耗。在内战时，44厂於1947年开发出一短木柄手榴弹，外壳加厚，破片效果增强，定为36式手榴弹。还有第30,31等厂大量制造。之外80厂另至一38式手榴弹，也是短柄近似无柄，弹体小到40豪米外径，近似瓶状。仅80厂制造。<br />
<br />
　　由联军的记载来看，志愿军使用两种手榴弹，仅弹体形状不同。极可能以这两种手榴弹(36式与38式)为主要生产形式。可惜的是杀伤力过低，战斗效果不佳。</div>

							
							
							
							
