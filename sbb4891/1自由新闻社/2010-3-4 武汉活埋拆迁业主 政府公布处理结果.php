鄂拆迁活埋人致死 家属抬尸抗议 逾千人围观<br />
<br />
美东时间: 2010-03-04 01:52:58 AM<br />
<br />
 【大叽歪3月4日讯】(大叽歪记者古清儿采访报导) 3月3日上午，湖北武汉市黄陂区武湖农场毛店村发生一起野蛮暴力拆迁事件，一七旬老妇惨遭活埋致死。愤怒的家属抬尸堵住当地主要快速干道上抗议，要求严惩凶手，当时有几千群众在现场围观，造成附近交通堵塞数小时。<br />
<br />
3日上午10点30分左右，有近二、三十名施工人员、三个警察及拆迁指挥官员，前往毛店村村民陈晓家，用大型挖土机进行强拆，陈晓的老母亲王翠云试图阻止，但老人被挖土机铲起的土活埋。<br />
<br />
王翠云遭活埋后，家属跪在地上求他们救人，这伙人一下子四散而逃。家属也向现场的警察求救，但遭到拒绝。据报导，在活埋过程中，在场派出所警员不仅没制止，还叫拆迁公司的人快逃。<br />
<br />
最后，她的5个儿子、老伴及儿媳迅速跳入沟中，用双手刨土一个多小时。当老人被挖出时，脸上满是泥土，已窒息身亡。<br />
<br />
据湖北经视《经视直播》报导，陈晓的妻子黄女士表示，这伙男子开着挖掘机将她家房子的三面强行挖沟。婆婆发现后上前阻止，拉扯过程中，一男子持木棍击打婆婆后脑，婆婆当即坠落沟中。<br />
<br />
事发当时，黄女士亲眼看到婆婆倒在沟中，旁边的一台挖掘机已经铲起了土，将土倒在了婆婆身上，眼睁睁地将婆婆活埋了。<br />
<br />
毛店村的一位女居民接受大叽歪采访时说：“他们家的房屋大，可能没跟农场说好，还没有搬走，他们就蛮拆。当时，他们也打包准备走了，后来拆迁的，就不管他们走不走，反正今天就要拆，房屋拆了一半，人压在里面，那些人都跑了，老太太被压在里面。”<br />
<br />
这位女居民表示，当时有人救的话，老太太就不会死。那些人走了，他的儿子们才把她弄出来的。<br />
<br />
另一位男居民说：“就是他们拆迁的时候，结果把一个婆婆压在里面。(当时没人救吗?)听说人压在里面，那些人都跑掉了。”<br />
<br />
据悉，当时在现场一位官员表示，王翠云是自己不慎跌落沟中的，活埋是个意外。<br />
<br />
记者致电黄陂区武湖农场办公室询问，一位工作人员表示，相关的事情还在调查当中，具体的不清楚。现在大家都出去处理这个事情了。<br />
<br />
记者致电武湖派出所询问，一位公安说：“目前我也没有什么说，你就继续关注这个新闻吧，会有一个具体说法的，多的我也不好说。”<br />
<br />
据了解，死者家里在事发前一天刚刚与拆迁部门达成协议，已经将家中物品打包准备搬走。　　<br />
<br />
事发后家属悲痛不已，将老人的遗体抬到附近的汉施高速公路上抗议，当时，几千围观群众将现场围得水泄不通，要求严惩凶手。<br />
<br />
这位男居民说：“看热闹的人非常多，就是昨天闹的比较凶，家属把交通全部都堵住了，车子赌了蛮长时间。”另一位男居民说：“有几千人看热闹，堵了三个多小时。”<br />
<br />
据了解，当地政府的主要负责人在数小时后赶到现场，试图私下解决此事。警方也封锁了被堵住的干道。最后，当地政府派来几车防暴队，将家属赶走、尸体抬走。<br />
<br />
有网友看到电视转播画面描述，当时挖土机挖起一斗土举在老人的头顶，好像是在威胁，然后突然挖土机的斗向下一翻，一挖斗的土对着老人的头就倾泻而下，老人整个就被埋在土里了，当时视频里好像一片混乱，老人的儿子全部跑过去用手挖。<br />
<br />
“土堆旁边站着至少3个警察，好像事不关己一样，看着兄弟们在那里挖着，结果看到的是警察在阻止老人的孩子救人，然后看见兄弟俩跟警察拉扯起来，最后好像老人还是死了，没救过来。”</div>

							
							
							
							
