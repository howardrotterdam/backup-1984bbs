													<h2>乌鲁木齐人：过去现在和未来</h2>
						
						
																											<div id="postmessage_152850" class="t_msgfont">这篇文章还挺客观的，转帖~<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 听到乌鲁木齐发生了暴乱，心里面一直无法平静，一天都在不断的搜寻各种信息，无法静心工作。因为这里，对我的意义，和大多数人并不相同――这里曾经是我的家园。<br />
<br />
　　网上言论铺天盖地，有过激的，有平和的，但是我想，对于这里大多数的人来说，那都是一个遥远的地方，遥远到甚至连想象都会出现两种极端，或者认为那里的人都在住帐篷，或者认为那里和其它城市没有任何不同。没有人知道那里是什么样子，即使是有心人，也只是从网上查一些资料，然后得到一些统计数据<br />
<br />
　　虽然已经有九年没有回去了，但是我仍然希望，能够以我的经历告诉大家一个真实的乌鲁木齐。<br />
<br />
　　在写这篇文章前，我本来想弄一幅地图出来，结果当我打开go2map时，却只能哭笑不得，是城市弄错了或者是地图的格局变成上东下西了？但是很快发现不是，城市还是那个城市，街道还是那个走向，只是街道名却全都错了。一个公认的国内地图竟然会将国内省会城市画错到如此地步，倒可以看出，乌市确实是令大家太陌生了。<br />
<br />
　　好吧，还是让我们打开google的地图吧。<br />
<br />
在现在的地图上,有一些地名没有标出来,但是却是乌鲁木齐的地标,<br />
<br />
　　南门:就是人民路和解放南路交叉口.<br />
<br />
　　北门:就是解放北路到方艺路交叉口.<br />
<br />
　　大十字:就是解放北路与中山路交叉口.<br />
<br />
　　小十字:就是解放北路与民主路交叉口.<br />
<br />
　　大西门:中山路上和新华北路的交叉口.<br />
<br />
　　找到这些方向标,你大概就能明白,乌鲁木齐的老城有多大了,这也是乌鲁木齐市的核心区,现在最繁华的地方,也都在这儿.<br />
<br />
　　我想大家经过这两天，对乌鲁木齐的资料应该已经知道一些了，乌鲁木齐，现有200多万人，算是一个不大不小的城市，各项统计数据放在城市堆里都不算显眼，不过在上世纪八十年代，有一项数据却和别的城市不同，那就是城乡人口比例，城市人口的比例达到80%。这个指标现在已经不算什么了，但是在那个年代，大多数城市（即使是北京），基本上都是城市人口少于农村的。<br />
<br />
　　这个80%的指标里面隐含着很多的东西。第一个，可以说明，乌鲁木齐是一个完全移民化的城市，而第二个，更重要，乌市更是一个几乎由平地生长起来的工业化的城市。只有在很短的时间，通过大量的投资，才可能造成这样悬殊的城乡人口比。<br />
<br />
　　实际上,自从49年中央政府进疆之后,便开始了持续的汉人进疆活动,一开始是王震的一兵团分赴新疆各地,然后就地驻防.<br />
<br />
　　不过在整个五十年代,似乎并没有大规模的移民活动,因为这个时间段来新疆的人,都基本是零散的,.我父亲的单位,有八千职工,五十年代来的很少,问起他们的经历,一般都会自称为盲流进疆.<br />
<br />
　　而占单位绝对多数的职工,则是六十年代来新疆的，这个时间段，大概是63年到66年间，因为66年之后，再来新疆的人，就没有全民所有制的正式工作了（家母正好赶上这个点，于是虽然有工作，却是集体所有制，一辈子都十分郁郁）。他们几乎有着相同的经历,即入伍、复员转业、不愿意回家乡、然后就来了新疆。<br />
<br />
　　而复员转业的军人，似乎来自各地的都有，但是最多的是陕西和四川，甚至到现在，老陕和川帮的斗争仍然在很多单位里反复出现。<br />
<br />
　　在六十年代之后，新疆的移民就基本上停止了，不过这批人是有组织的移民，而且带有半军事化的性质。<br />
<br />
　　现在，大家知道新疆有生产建设兵团，但是大多数人却不知道，当初新疆的兵团要比现在大的多。现在的兵团都是一些农场，又叫农垦兵团。而在80年代之前，新疆的大部分工业也是兵团建制的，新疆现在的很多局前身都可以追到工业兵团的某个师。可以说，新疆的汉人大都是兵团的后代。<br />
<br />
　　比如父亲的单位，是建筑公司，而在80年代的很长时间，都是以工一师工*团*营**连这样的名称存在，然后才改称新疆第*建筑公司*工区*队。虽然他们自从退伍之后就没摸过枪，但是这种建制，已经说明了他们的准军事性。<br />
<br />
　　当父亲们复员转业到新疆后，自然就遇到结婚问题，随之而来的就是托朋靠友，从内地介绍，而当时的政策，这种婚姻的女方，仍然是由国家分配工作，并且享受全民所有制待遇。政策的取消应该是66年或者是67年。<br />
<br />
　　于是，大批的家庭就这样出现了，随之出现的是婴儿潮，这一点倒和全国同步，在64年到78年间，无数的家庭以平均4到6个孩子的速度生育着中国的人口，一举将中国人口推过10亿大关。<br />
<br />
　　这批第二代，就是现在新疆汉人的主要组成部分。 <br />
<br />
　　对我们这些第二代而言，新疆就是家，而父母的出生地则十分遥远，虽然我们现在说起来，都会说自己是陕西人、湖北人或者四川人，但是，实际上，无论我们喜不喜欢，新疆的印迹都已经被留下了，因为一生的记忆都是从那里开始的。<br />
<br />
　　前面介绍过，最早的乌鲁木齐其实很小，城外就是戈壁滩了，好在有一条河从天山流下来，叫乌鲁木齐河，经过人工修筑之后，宽阔的河滩被约束成了和平渠，而原来的河滩则被改建成了公路，如果看地图，就会看见，城市被一条道路纵贯南北，这条路就叫河滩公路。而其实城市的发展，也是沿着南北展开。现在乌鲁木齐有很多个城区，而最主要的城区还是原来那三个，天山区、沙依巴克区、新市区。所有的事情其实也发生在这三个区里。<br />
<br />
　　在父辈们刚来到乌鲁木齐时，这里就是南门北门那么大的地方。自然容不下那么多单位，而各单位唯一得到的政策，就是城外的地方随便选，于是大家采用的差不多是跑马圈地的方式，划出各自的红线，然后在里面开始盖房子。从半截在土里的地窝子到土房到砖房再到楼房，即使你现在去乌鲁木齐，仍然会发现很多地名实际上是一些单位名，比如地质局、物质局、二建之类。我们一般都会称单位的住地为院子，也是那个时候留下来的。<br />
<br />
　　等到我们懂事时，乌鲁木齐已经很大了，北边越过了红山，发展出很大一片新市区，南边则一直可以到雁儿窝列士陵园。而实际看来，乌鲁木齐就是一座汉人的城市。在八十年代，曾经由政府下过一个通令，所有的牌匾上必须写上维族文字处理。这大概也是成立民族自治区之后为了尊重少数民族的结果吧。不过对于小商铺而言，想让装修工翻译出那些曲里拐弯的文字，确实很麻烦，所以现在的大街上，仍然是到处都只有汉文。<br />
<br />
　　我在这上面，好象多次说到八十年代，想一想，这个确实是最重要的一个时间点，在此之前，新疆的汉人的感觉中，维族人几乎不存在，因为他们都很老实，也很善良。甚至以后的很长时间，我们也都在说，是汉人把他们带坏了。父辈们流传着六十年代的一些传奇故事，都是说一只钢笔换一头羊，或者一个什么小物件可以换两面袋子杏子。但是自从八十年代之后，这一切都在慢慢改变，也许，今天的悲剧确实是那时候种下的。<br />
<br />
　　而在这之后，维族人在我们的心目中，逐渐变的凶恶起来。强卖现象就不说了，这是每个汉人都会遇到的。打架时成群上，不管有理没理。应该说，即使有很多抱怨，但是在九十年代之前，我们和维族人还是经常打交道的，到自由市场买牛羊肉，还有买葡萄干，还有好多土特产，都是和他们打交道。当时的说法就是，在他们那儿买东西，要就是问一下价不买，如果是讨价还价了就得买，不买的话，就可能打架。但是如果你狠一点，他们也不会怎么样。比如维族人的刀铺里，他们经常会拿着刀在你眼前比划，似乎是威胁，又似乎不是，反正好象你硬一点也就过去了。<br />
<br />
当时，在整个乌鲁木齐市，随处可见维族人，也随处可见汉族人。就象这次出事最严重的二道桥（就是国际大巴扎），当时，初中的我们，也是成群结队地过去玩。<br />
<br />
　　但是，当我2000年回去探亲时，发现一切都发生了很大的变化。当时闲着无事，就决定去我最熟悉的路上去走走。由二道桥，经解放路到南门，曾经是我们最常走的地方。然而这么长的一段路，竟然发现全是维族人，而很少见到汉人了，所有的店铺都是维族的，甚至连招牌都有很多只是维文。可以说我是在一种恐惧中走完这一段路的。而当我走到南门之后，再往前走，就出现了大片的汉人区。一街之隔，对比之明显，让人触目惊心。其实所谓民族的融合，说到底就是双方自由往来，那怕相互之间有矛盾都不要紧。而最差的情况，就是双方各自聚居，老死不相往来。而乌鲁木齐，经过多年所谓的民族团结局面之后，反而真正的形成了双方民族各自收缩，集中聚居的现象。<br />
<br />
　　说了这么多乌鲁木齐的历史，还是让我们回到google地图，看一看乌市现在的局面是什么样子。<br />
<br />
　　乌鲁木齐的道路中，最明显的一条，是外环路，大家找到外环路的南段，这是一条东西向的路，然后再找河滩路（南路）。以外环路和河滩南路的交叉点为中心，放大地图。然后，就可以看见更细致一些的布局，东面的第一条路，是新华南路，再往东，是解放南路，再往东，是外环路（东段）。再向北，找到人民路。维族人的主要聚居区，实际上就在新华南路、外环路（南段）、外环路（东段）、人民路这四条路的范围内，而解放南路，则是其核心区，如果你看了新闻报道，就会发现，所有出事的地点，也都是以这个范围向外扩展的。<br />
<br />
　　解放南路，由外环线到人民路，是很长的一段，前面说了，解放南路上已经看不到汉族人了，但是不幸的是，很多辆公交线路还是会经过这里，更不幸的是，当时正应该是下班时间。我很难想象出当时的惨境，那些能够躲到武警队伍里的，是幸运者，或者说，在解放南路上的，恐怕还多数是幸运儿，而那些在小街巷中行走的人们（这个圈的外沿，仍然住着很多汉人），才是真正的受害者，当暴徒们向他们涌来时，也许他们已经有了本能的警惕和畏惧（这种本能是几十年生活的经验），但是当英吉莎小刀划破他们的喉咙时，他们会想到什么？也许，这就是命吧。<br />
<br />
　　暴乱发生之后，就不断打电话给那边的人，很多人都不知道发生了什么事儿，因为包括我们大多数人，都没有想到会死这么多人。这边炸两个车，杀一两个人是常事儿，但是聚众**发生骚乱，好象还是自八十年代最后那一年之后的头一次。<br />
<br />
　　那一年的事，其实不用说了，内地闹的更凶，新疆自然也一样。大家都在人民广场示威，不过，当学生们发现，竟然有几千维人也来凑热闹时，就知道势头不对，不能和他们搅和，立即撤回学校去了。而果不其然，没多久，维人们就忍不住本性，冲进了市政府，将所有的东西砸了个稀烂，也由此开始了这里长期的动荡过程。<br />
<br />
　　ZXB说，这次行动是由境外组织策划的暴力活动，这个结论真的不太对。要知道，在现今的世界局势下，想通过针对平民的暴力来促成地区的独立，不但达不到结果，反而只会起反作用，让那些支持者们都无法再支持。科比娅老奶奶，毕竟还是个上等人，大概确实只是想让新疆的维人们，站出来，表明一下态度，起码不能对内地的那件事毫无反应，因为这也不附和维族人的生活方式。但是，要说，科比亚就是要让维人去杀汉人去把乌鲁木齐砸烂，这个就是十分的错误了。因为，在我看来，这短暂示威之后的不受控制的暴行，恰恰是街头维人的本性暴露，因为，由古至今，他们还从未有过不把和平示威变成一场暴行的先例。<br />
<br />
　　暴力之所以发生，就是因为其有不可避免性，还因为，其实这样的暴力活动每天都在发生，只是分散的，而7.5日，则是集中的。街头几个维人将一个汉人打的死去活来，然后拍拍屁股走人的事儿，从八十年代就开始了，从开始的义愤到现在的麻木，都已经成为新疆的一大常态。这也是我们举家东迁的重要原因。看来我们走对了，因为这一次，我们终于可以不担心家里人的安全了。<br />
<br />
　　前面说过，所有的改变都开始于八十年代。对少数民族的倾斜政策，是全面的，从工作职位安排，到高考加分，再到底层的“少抓少杀从宽处理”。其实这些政策，对于个体的有利，却造成了对民族整体的全面侵害。尤其是“二少一宽”，要知道，每个民族都有败类，对这些败类的清除是本民族的一种优胜劣汰。而如果针对民族之间搞什么不平等的话，受益的是坏蛋，而受损害的整个民族。在河里的争论中，无数的人为民族倾斜政策鸣不平，说这对于汉民族是不公平的。但是，如果你去新疆看看，就知道，这些优惠政策是如果在多年之后，使得维人作为一个族群彻底地丧失了社会能力的。<br />
<br />
　　到新疆，有一个现象很有意思。那就是汉族的节日，比如春节、中秋什么的，维人也会放假，而维人的节日，比如古尔邦节（汉人放一天）、肉孜节，维人放假，汉人不放假。大家都会说，这真他妈不公平，但是仔细想一下，才会发现这里面竟有一个惊人的秘密。因为这个现象说明，即使维人放假了，汉人依然可以继续工作。也就是说，在新疆，一切活动都可以不依赖维人的参与而正常进行。<br />
<br />
　　而再实地的考察一下，你就会发现，所有的工矿企业，领导层中，都会有一个少数民族。这个指标是定的。但是，也只会有这么一个少数民族。这个人其实处于十分尴尬的局面。那时我曾经在炼油厂工作，在基层员工中，几乎很少有维人，即使有，也是十分熟练的汉语，而那个维人的厂长助理，在讲话时坚持用维语，可想而知，他能在厂子里获得多少威信。<br />
<br />
　　在乌鲁木齐、克拉玛义、奎屯，只要有工矿企业的地方，就是汉族占到80%以上。而更有意思的是，主要的岗位都是汉人占据，在几乎所有的企业里，维人都是很特殊的，他们可以不请假就不来上班，而且不扣工资。因为大家都是觉得有他不多，无他不少。而这些维人，其实都是维人中最出色的人，他们都是经过大学出来的。<br />
<br />
　　实际上，正是这种所谓的优惠，造就了现在这种个体上占优，而整体反而被排挤的现象。我觉得这种优惠政策在很多国家似乎都造成了反作用，就比如法国对于解雇劳工的约束。优惠政策实际上将一个群体的弱势给突现出来，让主体社会见到这个群体，就会想到他们的问题，而不是作为个体区别对待。在新疆也是一样，企业招工，是需要考虑民族，但是所有的企业都会以这个政策作为上限，多一分优惠也不加上去。比如企业，15%必须是少民，那就是15%，养着他们就行了。其实如果是计划经济，这样还会有成效。问题现在国家是以私营企业为主了，私营企业绝不会管政府的这些规定的，除非是政府给好处。就象这次的韶关事件之后，我想就再没有企业敢去新疆招工了。人家是来赚钱的，不是来给自己找麻烦的。<br />
<br />
　　维人的传统是农业和商业。但是正是因为“二少一宽”，造就了汉人对于维人强卖的印象，而这种印象造成汉人根本就不和维人做生意。而工业的发展，也严重压缩了维人的商业活动，维人的商业，主要是土特产、手工艺品。但是1998年，我回家时注意到一个奇怪的现象，原来遍布大街小巷的土产批发店，几乎没有了，改换后的门面，基本是买工业品的。仔细一想也是，一店的葡萄干能顶的上几筒油漆的利润。自此之后，维人的商业活动逐渐集中在自由市场中，并且越来越集中在那么有限的几种商品上。<br />
<br />
　　在新疆的大西门批发市场，还有上次着火的国贸城中，还有整个火车站附近的几大批发市场里，内地来的商贩们，最早以一个床位一个床位的方式批发零售各种服装、小手工品，现在大的已经开了店面。可以说，这是个纯汉族的领地，汉人的第二代们，其实也没有铁饭碗可端了，但是大家从这里批发东西，然后开各种店面去卖，新疆各地的商人，也到这里拿货。这是一个完全越过当地经济结构的商业网络。但是背靠这一经济网络，你可以想象汉人将比维人拥有多大的优势竞争力。<br />
<br />
乌鲁木齐的暴乱就是这样的，ZXB说的对，确实是一小撮，3000暴徒，如何能代表800万维人，要知道，任何一个民族，特别是农耕民族，能够举刀杀人的都是极少数（所以即使战争时期多的也是炮灰而不是英雄）。但是，没有人会这么看，就象我们见了姚明得好处我们也瞎高兴一样，我们见了这些维人杀汉人，就会把帐算到所有的维人头上。甚至即使我自己对我说，维人大部分是好的，但是下次再见到维人，我还是会由心底里生气。我想，新疆的汉人肯定会是这样的想法，而且新疆的维人感觉到这种想法之后只能会和汉人更远离，即使那些想亲近的也不会，因为人都有尊严。为什么兵强马壮的南黎巴嫩军在以色列撤离之后连一天都挺不住，这是因为他们根本就没有尊严，这样的人也被我们称为汉奸。<br />
<br />
　　所以，这一场的暴乱无论对汉还是维都是一个悲剧。其实维族的独立意识根本就不怎么强，因为历史上他们也重未建立过一个国家。我们经常说库尔德人的悲剧，拥有5000万人口的民族却没有自己的国家，而维族也差不多。所谓民族独立意识的增强，是二战之后美国体制下的产物，大家有没有发现，二战之后小国家越来越多，而且大的国家还有往小里拆的想法。甚至出现了独立的另外一种潮流，就是发达地区希望独立出来，不和那些穷哥们一起过（比如意大利北部）。在新的世界格局中，领土完整成了神圣不可侵犯的，这其实造就了几千年来的大变局，弱小民族不再需要托庇于强势民族，也能够生存，而且小国家更易生存，起码对于上层领导者来说，做一个国家元首肯定比做一个省长强。可以说当今世界的很多分离意识都与此有关。<br />
<br />
　　但是对于普通老百姓，生活还是第一位的。宣传很重要，如果你的周围全是说独立好，汉人拿了我们的地，拿走我们的油，拿走我们的棉花（这些也全有事实依据），你发现自己现在没有工作，只能在街头混，那么我想，只要是有思想的人，都会生出悲愤之情的。而乌鲁木齐的解放南路，恐怕充满的就是这样的声音。那儿买的书我都不认识，但那种情绪能够感受到，我能看到的就是那儿新修了很多寺，但是整条街的建筑，几乎还都是以前的老样子。有人问，那儿难道没有警察，说实在话，真没有看到，问题是，你在汉人的城市里，走过三个街区，又能看到几个警察。<br />
<br />
　　族群的形象是对外的，在一个族群里，一样会分出无数个小群体。我们前面已经说了，每一个族群都有街头少年，也有学校少年。汉族的街头少年比之维族，其凶恶程度一点也不差。问题就在于，如果这个社会，将族群看淡，那么整个社会，先分出来的就是街头少年和学校少年。而不论行为，先看族群，那么我们知道的就是，某族人在杀人，而最终的后果，只能是族群分裂。现在的新疆，恶果已经结成了，可是那些肉食者们，仍然将头扎在沙子里，喊着什么安定团结来之不易。<br />
<br />
　　暴乱就是暴乱，暴乱就有暴乱的解决方式。以霹雳手段，行菩萨心肠。韶关的领头者要杀，而乌市的暴乱中所有的杀人者，也一样要杀。只有这样，才能将族群的概念淡化掉。法律面前，人人平等，这才是将天平摆回来的根本做法。问题是，有司们，能做到吗？<br />
<br />
　　前面，我曾经发了一篇聊斋上的小文《盗户》，现摘给大家看看。“顺治间，滕、峰之区，十人而七盗，官不敢捕。后受抚，邑宰别之为‘盗户’。凡值与良民争，则曲意左袒之，盖恐其复叛也。后讼者辄冒称盗户，而怨家则力攻其伪。每两造具陈，曲直且置不辨，而先以盗之真伪，反复相苦，烦有司稽籍焉。适官署多狐，宰有女为所惑，聘术士来，符捉入瓶，将炽以火。狐在瓶内大呼曰：‘我盗户也！’闻者无不匿笑。”<br />
<br />
　　将这个盗户，改名维人，是不是就是现在的状况。“二少一宽”的政策错了，纠正是必须的，但是以后呢？多一事不如少一事的庸官做法能变吗？其实何止是民族矛盾，我们的葫芦官们，又判了多少葫芦案子，远的不少，最近福建的那个医闹的案子，不也是这样。<br />
<br />
　　“天下之患，最不可为者，名为治平无事，而其实有不测之忧。坐观其变，而不为之所，则恐至於不可救；起而强为之，则天下狃於治平之安而不吾信。惟仁人君子豪杰之士，为能出身为天下犯大难，以求成大功；此固非勉强期月之间，而苟以求名之所能也”。<br />
<br />
　　上文是摘自《晁错论》，其实我们的国家，无论成立时间还是发展，都已经到了汉景唐玄的那个时代，第一代打天下的老人已经谢世，而下一代承平日久，见血光不知所往。二战的老兵们，共同的特点，就是不好战，也不怕战，因为他们知道战争是怎么回事，也知道在何时运用，知道生命珍贵，不是韭菜，但是更知道，要想获得持久的和平，有些人的头，就是必须要拿来示众的<br />
<br />
　　其实上一代人已经将那些血腥的事情做了，给我们打下了坚实的基础，要我们做的，就是将那些公平的政策执行下去。“所有的族群一律平等”，同时尊重少数民族的各项权利。而最重要的是，国家更应该知道，沉默的大多数是什么人，哪些人，才是我们这个统一的多民族国家的柱石。</div>

							
							
							
							
