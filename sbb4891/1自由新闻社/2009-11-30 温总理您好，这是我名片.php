“温总理来了！”昨天下午，南京工业大学图书馆成为全校的焦点。到南京参加第十二次中欧领导人峰会的国务院总理温家宝在省领导梁保华、罗志军等陪同下来到南工大丁家桥校区，考察了该校材料化学工程国家重点实验室，并且到图书馆看望了大学生们。温总理的亲切、睿智、幽默与博学，令全场不时发出阵阵掌声与欢笑。<br />
<br />
　　温总理与大学生们亲切交谈，现场气氛热烈。有同学“抢”提问机会，吸引了大家的目光。 快报记者 路军 郑春平 摄<br />
<br />
　　“总理来到了我们学校”<br />
<br />
　　听说温总理要来，图书馆里的温度顿时显著上升！楼上的阅览室按规定要保持安静，但是同学们实在难掩兴奋，三三两两同桌的，压低了嗓子传递着这个好消息：“总理很快就到！”<br />
<br />
　　说到就到！只听见一楼大厅传来一阵阵掌声与欢呼声：“总理好！”“欢迎温总理！”……这时候，楼上阅览室的同学们一个个都忍不住了，呼啦一下子全都涌到了走廊里，围着栏杆一边拼命地鼓掌，一边大声向步入大厅的总理问好。细心的温总理显然也听到了楼上同学们的问候，抬起头微笑着向大家挥手致意。<br />
<br />
　　大厅里早已布置好了翔实的展板，介绍校方及材料化学工程国家重点实验室的系列科研成果。温总理在详细听取了汇报后，上楼到图书馆看望同学们。<br />
<br />
　　当总理真真切切地出现在大家面前时，现场出现了一刹那的安静，继而爆发出持久的雷鸣般的掌声，“真的是总理来了！”“总理好！”坐在每一个角落里的同学都向着总理聚拢，转眼工夫就把总理围得里三层、外三层。总理微笑着与大家打招呼、热情地与师生们握手……<br />
<br />
　　大学生老板――<br />
<br />
　　“总理好！这是我的名片”<br />
<br />
　　“温总理，您好！这是我的名片……”总理还没站定，技术学院的学生刘勇就挤上前去，向总理递过去一张自己的名片。总理笑了，他欣然接过名片，并且跟刘勇握了握手。刘勇抓住机会向总理表达自己的想法：“非常感谢国家和地方政府对大学生创业的支持。希望今后能出台更多更好的政策！”<br />
<br />
　　今年大四的刘勇已经当上了老板，他和另一位同学7月份刚刚创办了“南京好吉利文化传播有限公司”，目前主要从事动漫服装的设计、制作、销售与租赁业务，生意很不错。公司是在南京浦口高新开发区注册的，享受了大学生创业减免费等诸多优惠政策。“温总理在很多场合强调过大学生就业、创业问题。我就是想借这个机会，一来表示感谢，二来希望好政策能出得更多！”<br />
<br />
　　像刘勇这样关心就业、创业的同学还有很多。温总理对大家说，“对即将工作的大学生，我们会想方设法让他们能够有施展才能的地方。但是大家知道，今年和明年，大学毕业生都达到了700万人，而每年的就业计划安排也就是900万人。今年经济指标好，可能达到1100万人，但社会上还有其他需要就业的人员，在这种情况下，我们希望大学生珍惜自己的工作来之不易，不管将来到什么工作岗位，都要充分发挥自己的才能，献出全部的力量，为人民为国家做出贡献。至于国家在就业政策方面，将会采取一系列扶持措施，现在这样做，将来还会这样做！”<br />
<br />
　　为总理拉椅子――<br />
<br />
　　“总理让我坐他旁边”<br />
<br />
　　大家恨不得都能跟总理说说心里话，挤不进来的同学急坏了。还是温总理理解大家，他微笑着说：“同学们，我坐下来和你们说说……”<br />
<br />
　　总理话音还没落，站在阅览桌旁的大三学生王海宁已经飞快地为温总理让出了位置并拉开了椅子：“总理，您请坐！”<br />
<br />
　　“谢谢。”总理见状笑着对她点点头：“你坐我旁边。”<br />
<br />
　　王海宁不禁又激动又紧张，直到总理又说了一句“你坐我旁边”，这才坐了下来。满场的同学这下可羡慕坏了，“能坐在总理旁边，太幸福了！”<br />
<br />
　　坐定后，温总理首先表达了歉意：“非常抱歉，不能和大家多呆一段时间。如果可以的话，我就和大家一起在食堂吃顿饭了……”听到总理如此亲切的话语，同学们报以更加热烈的掌声，大声喊：“好！”“谢谢总理！”<br />
<br />
　　“你们都是毕业班的学生吗？”总理问。“各年级都有，还有研究生、博士生……”同学们七嘴八舌自我介绍起来。<br />
<br />
　　“刚才看了学校的一些科研成果，我感觉我们这所大学是注重理论实际联系的，把产学研结合起来的做法很好……”说完这些话，总理主动问大家：“同学们有哪些问题要问我？”顿时，现场齐刷刷地竖起了众多手臂……<br />
<br />
　　永远难忘――<br />
<br />
　　“我给总理戴上了校徽！”<br />
<br />
　　第一个提问的是化工学院大四学生杨玉琦，坐在温总理对面的她手举得最快。温总理鼓励她说，“你很勇敢。”<br />
<br />
　　“温总理您好，能否请您对大学生党员提一些建议和要求？”杨玉琦是今年9月份刚转正的新党员，又刚入选06级第二学生党支部书记，特别想跟总理这位老党员“取取经”。<br />
<br />
　　“我也是在大学期间入党的。我当时对自己最大的要求就是要处处起模范带头作用，不管是在学习上，还是对待将来的工作上，都应该做出表率。”温总理语重心长，希望大学生党员一定要努力学习，团结同学，不仅要关心自己的成长，还要关心集体的成长。<br />
<br />
　　听着总理的嘱托，杨玉琦不住地点头，同时也暗暗下决心――等会儿总理告别时，一定要把自己带来的一个小礼物送给他。这个小礼物不是别的，正是南京工业大学的校徽。这是去年学校安排去香港、澳门交流时制作的礼品，比平时佩戴的校徽更精致、特别，一年多以来她一直珍藏着没舍得戴。<br />
<br />
　　“总理，请接受我们一点小小的心意！”打定了主意，在总理起身准备告别时，杨玉琦赶紧掏出校徽请总理收下。总理非常高兴地说：“请你帮我戴上吧！”大家一听，不约而同地鼓掌并齐声叫好！<br />
<br />
　　因为太激动了，杨玉琦拿着校徽的手不免有些抖，校徽背面的别针第一次没有扎上。她还特地问了下总理：“这个校徽的别针要扎透衣服的，可以吗？”总理一点都没犹豫地说：“可以啊！”<br />
<br />
　　现场认本家――<br />
<br />
　　“我姓温总理的‘温’”<br />
<br />
　　“温总理您好，我叫温建锋，读研究生三年级……”好不容易“抢”到了提问机会，温建锋首先向总理认起了“本家”：“总理，我也姓温。”现场响起一片笑声。<br />
<br />
　　“是和我一个‘温’吗？”温总理也笑着问。<br />
<br />
　　“是的。我作自我介绍时都说自己是温总理的‘温’……”这一下，笑声、掌声，现场气氛更加热烈。<br />
<br />
　　温建锋是机械与动力工程学院的研究生，准备继续读博士。他说，一直以来自己特别关注我国科技的发展，虽然不乏出色的科技人才，但是至今还没有一个获得过诺贝尔奖的。<br />
