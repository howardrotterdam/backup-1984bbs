　　“我冲出病房，刚好见到一个医生：‘大夫，我妻子没死，你们快救救她。’医生像见了怪物一样挣脱我的手快步离开。我又找到一位护士：‘快去找院长，我妻子还没死。’护士进了一间病房，把门反锁上任凭我怎么敲。<br />
<br />
　　“我从一楼跑到二楼，二楼跑到三楼，见到医生便跪在地上哀求：‘求求你救救我妻子，她还没死，还能说话……’我的头在地上磕得直响……我跑遍了医院的所有楼层，不知给多少医生护士下跪……后来，全院上下竟然见不到一个医护人员。我跪在院长办公室门口，使劲拍打着办公室的门，可没有一个人出来帮忙。”<br />
<br />
　　这时，妻弟哭着跑来说：“姐夫，我姐快不行了。”唐泽志跌跌撞撞往病房跑去。 <br />
<br />
　　此时，裴传琴已经陷入昏迷，血打湿了白床单直往下滴。唐泽志跪在妻子身边颤抖地说：“传琴，我是泽志，你醒醒，医生就来了。”裴传琴微微动了一下手，眼泪顺着眼角流了下来……这次，妻子真的走了。<br />
<br />
　　院方赔付死者亲属31万元<br />
<br />
　　妻子死后，唐泽志心里充满了对医院的怨恨和愤怒。<br />
<br />
　　当天下午3时，在没有一个医护人员出面解释的情况下，唐泽志冲进一间办公室，砸毁了两台电脑和一些办公设备。巨大的响声惊动了躲避的医护人员，一个个又都冒了出来，并拨打了110。<br />
<br />
　　此时，唐泽志总算见到了院长，对方低着头不敢看他的眼睛。<br />
<br />
　　产妇故去两天，医方对于裴传琴的死亡原因一直没给出确切说法，甚至连个道歉也没有。医院冷漠的态度激怒了唐泽志的家人，他们打着横幅在医院门口静坐。<br />
<br />
　　2010年7月30日，当地一位领导出面，将唐泽志和医院方面的代表召集在一起，商量具体解决办法。<br />
<br />
　　医院辩解，当天院方对裴传琴进行抢救，确认其生命体征完全消失才宣布其死亡，病人“死而复生”是病人家属过度伤心产生的幻觉。<br />
<br />
　　2010年8月1日，平安医院与唐泽志进行第二次协商。医院表示，在这起事故中，院方愿意承担一定责任，同意赔偿死者家属27万元。<br />
<br />
　　事发后，笔者采访医院的医护人员但遭到拒绝。一位不愿透露姓名的医生说：“不是我们医生见死不救，只是大夫属于不同科室，要是出了什么意外，谁来担这个责任。”<br />
<br />
　　2010年8月6日，在不申请医疗事故技术鉴定委员会对死因及责任进行鉴定的前提下，在含山县环峰镇政府调解和含山县环峰镇法律服务所的见证下，唐泽志与医院就裴传琴死亡赔偿一事达成协议：2010年7月28日，申请方的亲属裴传琴(女29岁)在被申请方处生产，产妇在剖宫术过程中出现失血性休克症状，经抢救无效死亡，双方为此产生医患纠纷。<br />
<br />
　　在不申请医疗技术鉴定委员会对死因及责任进行鉴定的前提下，裴传琴死亡赔偿一事经调解双方自愿达成如下协议：<br />
<br />
　　被申请方(医院)赔偿申请方(因裴传琴死亡)所有损失共计人民币叁拾壹万元整。<br />
<br />
　　双方达成赔偿协议后，申请方不再因财产损失追究被申请方赔偿和其它责任。<br />
<br />
　　协议签订后，裴传琴的善后事宜由申请方负责。<br />
<br />
　　本纠纷作终结调处，以后双方当事人及其亲属不得再以此事为借口找对方闹事。<br />
<br />
　　近日，院方已将31万元损失赔偿给付完毕。张 颖</div>

							
							
							
							
