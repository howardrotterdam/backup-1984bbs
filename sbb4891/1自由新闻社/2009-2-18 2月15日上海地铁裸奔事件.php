													<h2>2月15日上海地铁裸奔事件</h2>
						
						
																											<div id="postmessage_33809" class="t_msgfont">2009年2月15日，晚上7点地铁路径陕西南路站，停站后，忽然面前的人开始狂叫，刹那间，3节车厢人都没了，就看到一个全*人，大约26岁左右，坐在座位扶栏旁边，（旁边一件羽绒外套，一个内衣，一条黑色裤子，一双鞋子，一把阳伞），抱着男人的大腿不放，起初，我以为是两个人吵架，还没等我反应过来，这男人拖着女人走了几步，便从*手中挣扎出来，逃走了，2个人应该是不认识的。<br />
<br />
于是*就开始在车厢里来回走，车厢又开始大叫，然后她又回到扶栏旁边，躺在地上，然后一个上海阿姨，大概50岁左右，拿着她的羽绒服试图盖在她身上，还对她说“小姑娘，穿起来，这么多人，很难看的。”，*把衣服甩开，一会地铁的警察来了，也企图给她盖衣服，*不从，警察商量后把她拖下车了，上海阿姨最后上车说，警察说这女的已经不是第一次这样了，整个过程大概2分钟左右。<br />
<br />
心理专家点评：<br />
<br />
据不完全统计，近来仅媒体披露的“裸奔”事件就有十余起，这些事件千奇百怪，分析原因，裸奔者或心理过于脆弱，或精神上遭受了重压。从精神医学研究角度看，裸奔是一种行为紊乱现象，是病人在精神疾患影响下所产生的一种异常行为，其中最为常见的是精神分裂症。 　　<br />
<br />
人都有各种需要，某些需要在社会正常的伦理道德下不能满足时，会给人的生理和心理造成损伤，产生一定的不良情绪。如果受到一定的外界刺激，这些不良情绪就会被释放出来，有时表现为思维障碍和行为异常。裸奔就是多种多样的异常行为中的一种极端现象。 　　<br />
<br />
表面上，裸奔者往往是在经历了失业或失恋等破坏情绪，在遭受了外界刺激时，才导致了行为紊乱。但外因只是一种诱发性因素，真正起作用的还是内因。产生精神分裂或行为紊乱，常常与一个人的个性或身心某方面的缺陷有很大关系。<br />
<br />
裸奔者并非全都是精神异常。有些人裸奔是向梦中偶像示爱，或借以表达自己的倾慕之意。今年温布尔登网球公开赛上，库尔尼科娃的崇拜者，那个光溜溜的罗伯兹，裸奔了９０秒才被制止，而这已经是此人第１５６次作出此种惊人之举了。 　　<br />
<br />
当今社会，竞争性与挑战性大大增加，这就要求社会个体应具备更好的心理素质和更强的承受挫折的能力。人们的愤怒、失望、烦恼等不良情绪应当得到健康的疏导与发泄，这样才会有利于社会的健康发展。人们只有在一个健康的社会里生活，才能更好地维护自己的身心健康。<br />
