													<h2>温家宝看望浙大师生 勉励学生要做事不要做官</h2>
						
						
																											<div id="postmessage_705936" class="t_msgfont"><a href="http://news.163.com/10/0626/19/6A4J6VD6000146BC.html" target="_blank">http://news.163.com/10/0626/19/6A4J6VD6000146BC.html</a><br />
<br />
2010-06-26 19:03:31　来源: 中国广播网(北京)<br />
<br />
核心提示：中共中央政治局常委、国务院总理温家宝25日晚来到浙江大学看望慰问广大师生，并与大学生座谈。他勉励同学们要大胆求知，务求实学，不骛虚声、不求虚名，以科学的态度学习做踏实的工作，做对国家和社会有贡献的人，人民记得住的人。<br />
<br />
中广网北京6月26日消息 据中国之声《全国新闻联播》18时38分报道，中共中央政治局常委、国务院总理温家宝昨天（25日）晚上来到浙江大学，亲切看望慰问广大师生并与大学生座谈。他勉励同学们要大胆求知，务求实学，不骛虚声、不求虚名，以科学的态度学习做踏实的工作，做对国家和社会有贡献的人，人民记得住的人。<br />
<br />
6月25日晚上八点钟刚过，浙江大学的图书馆里，同学们正在看书学习，为即将到来的考试做准备。国务院总理温家宝走进图书馆。他刚刚结束了在杭州一天的视察走访，不顾劳顿，前来看望浙大的广大师生。同学们看见总理来了，惊喜之余纷纷聚拢过来。<br />
<br />
温家宝：我今天晚上是突然袭击。<br />
<br />
学生：太突然了！<br />
<br />
温家宝：我六点多才回来，书记省长问我有安排没有？我说没有。然后吃完晚饭我告诉他们，我说我要到浙大去！我主要是惦记你们，来看望你们。<br />
<br />
温家宝和同学们坐下来，详细询问他们的学习生活情况。同学们请他讲点什么，温家宝思考了一下，说：谈些什么呢？还是从你们的校训求实谈起吧。<br />
<br />
温家宝说，浙江大学的老校训是 “务求实学，存是去非”，求实是基础，这种校风一直延续到现在。浙大的历史是一部光荣史，人才辈出，有160多个院士出自浙大，这是同学们引以为豪的。但是前人的贡献不等于你们的贡献，你们要达到前辈的水平还要做艰苦卓绝的努力。<br />
<br />
温家宝：同学们要有无穷的求知的欲望，要做踏实的功夫。不骛虚声、不求虚名，惟以科学的态度做踏实的工作，这些都可以作为理解你们校训的格言，终生受益。<br />
<br />
<br />
大三学生汪寅龙对温家宝说，他想从政，想做像总理一样为老百姓服务的人，想请总理给他一些建议。温家宝说：要做事不要做官。<br />
<br />
温家宝：我们人生的目的就是要为老百姓办事，无论干哪一行、从事什么专业，都要用你所学的专长为人民服务，你这样做了，而且做了贡献了，人民会记住你的；相反，如果你即使当了官，但是你不是为人民服务，而是为做官而做官、甚至背离老百姓的利益，那对人民是有害的。为天地立心，为生民立命，为往圣继绝学，为万世开太平。这应该是我们的理想，我们也应该脚踏实地去实践。如果是这样的政治家、这样的官，老百姓是欢迎的。<br />
<br />
听了总理的教诲，图书馆里掌声经久不息的响了起来……<br />
<br />
