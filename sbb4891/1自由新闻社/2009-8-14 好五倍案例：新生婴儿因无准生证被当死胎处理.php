<br />
现代金报8月13日报道 要不是那本发黄的旧病历，陈女士到现在都不会知道，自己在10年前曾经产下过一个男活婴。更让人心寒的是，那个男婴在出生两分钟后因为某种原因夭折，然而当时医生却告诉她生下的是一个“死胎”。然而，陈女士手中10年前的产科病历却记录：因无准生证，故未予清理呼吸道分泌物处理，2分钟后新生儿死亡。<br />
<br />
目前，陈女士已将相关材料送到了信访和司法部门。<br />
<br />
10年前<br />
<br />
她产下“死胎”后从此不育<br />
<br />
陈女士家住宁波市鄞州区某街道。<br />
<br />
前天，记者在宁波市信访办大厅见到陈女士时，她正在桌子旁整理手中的材料，脸上还挂着泪痕。10年前，陈女士还是宁海农村一个姑娘，在和相恋4年的恋人苦苦拼搏多年后，他们原本计划在宁波买好房子后再结婚生子。1998年，当他们终于买了房子时，陈女士却发现自己怀有两个月的身孕，结婚势在必行。陈女士既欢喜又犯愁，因为当时婚检很严格，如果两人领证结婚，必须进行婚检，万一被发现怀孕了，孩子保不住怎么办，鉴于这种担心，夫妇俩当时没有去领证结婚。二人商量，按农村习俗先办酒席举行婚礼仪式，随后再补结婚证。办了婚礼仪式之后，夫妇二人也做好了一旦孩子出生，因无准生证他们将面临处罚的心理准备。<br />
<br />
陈女士十月怀胎(病历写40周)要临产时，家人把她送到了宁海第一医院妇产科生产。产后，她和家属没有见到孩子，当时她们被医院告知，生的是死胎。听到此消息，陈女士处于悲痛之中，当时大家都劝说她还有机会再生一个孩子。也就是这个希望，成了陈女士从悲痛中走出的精神动力。然而谁能想到，从此以后，她10年也没能怀上孩子。<br />
<br />
10年间，她求医问药只想有个孩子<br />
<br />
一天天过去，一年年过去。看着马路上一些蹦蹦跳跳的小孩子，陈女士越发不是滋味，夫妇俩开始试着到医院看病，想尽快要个孩子。宁波市第一医院、宁波市妇儿医院、宁波市中医院、江北慈城的保黎医院……只要有人说哪个医院有妇科专家不错或是吃什么药比较管用，她都会去试试。最后还用上了一些土方，不过依然没有效果。一次次希望，又一次次的失望，陈女士在10年间备受煎熬。用她自己的话说，就连做梦都梦到自己有孩子了，但醒来之后，现实依然没有任何改变。<br />
<br />
陈女士家庭条件一般，经济来源依靠在临海做加工生意的丈夫支撑。即便是这样一种环境，为了要孩子花再多钱，陈女士夫妇都不后悔。10年下来，他们花费了十几万元。两年前，夫妇俩还曾把试管婴儿作为自己最后的希望。<br />
<br />
10年后，她发现当年病历上有惊人秘密<br />
<br />
试管婴儿。陈女士怎么也没有想到，自己会走这条路。<br />
<br />
今年7月份，她在宁波某医院人工授精第三次失败，但这依然没有打消她要宝宝的念头。当时，医生要求看陈女士以前的生育病历，于是在2009年7月24日，陈女士从宁海第一医院取出了她曾在这里住院生产的部分病历。病历拿在手中，用陈女士的话说，“翻开看也看不懂”。所以她当时并没有从病历中看出什么。<br />
<br />
大约在7月31日，有位专家查看病历后发现了一个惊人秘密，那就是陈女士10年前生的是一个男活婴。陈女士在宁海第一医院住院生产的部分病历上，其中在1999年4月5日的“手术记录单”中的内容格外详细。在手术人员一栏中涉及到“蒋、王和姜”三个不同姓氏的妇产科医生；在手术后诊断一行有“死产”字样，死字加粗。其中在手术经过说明中，蒋医生记录得很详细，部分内容显示：“分娩一成熟男活婴(无准生证，县×××陈××来院不准新生儿存活)故未予清理呼吸道分泌物处理，2分钟后新生儿死亡。”<br />
<br />
得知这个事实后，陈女士崩溃了。如果不是把病历拿出来，她竟然不知道自己在10年前曾经产下过一个活生生的儿子。采访中，陈女士始终哽咽着，“为什么没能让我的孩子活下来？”陈女士说。<br />
<br />
