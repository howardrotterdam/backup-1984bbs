																											<div id="postmessage_91226" class="t_msgfont">看完了《劫后天府泪纵横》<br />
兰小欢 @ 2009-5-12 9:10 评论(9) 推荐值(348) 引用通告 分类: 未归类<br />
<br />
今天早上，打电话给有线电视公司，让他们开通我的HBO频道，为了收看这部39分钟的纪录片《劫后天府泪纵横》。关于这部纪录片的介绍，点击这里。从现在开始的最近的一个月里，几乎每天都播。 有条件的朋友，这里是节目播放时间表。<br />
<br />
听说去年国内铺天盖地的报道就是一个字：悲。这也是这部影片前半部的主题。然而悲愤悲愤，那个“愤”呢？大家也许还记得那位跪在路中间试图阻拦愤怒的家长们到成都上访的市委副书记，媒体给了很多报道，如今大约已经高升了。他当时说的是：“我为你们主持公道，一定彻底调查。”这部纪录片拍下了人群中一位母亲的愤怒回应：“调查你妈逼啊！”那是地震一周后无人问津无人关心后的怒吼。<br />
<br />
调查你妈逼啊。。。现在他妈逼也不让调查了。于是，“死于非命”这个表示愤怒和震惊的成语变成了无奈的一声叹息。<br />
<br />
我忘不了影片中阻止家长上访的官员：“现在有外国朋友们在场，大家要保持形象啊！”<br />
<br />
我忘不了为平民愤而拍到现场的建筑专家，这里瞅瞅那里看看，一位当地人指给他们看各种建筑废墟，专家说：“这个好像是有些问题，薄了一点。”然后拿出小本子比比划划，那位当地人调笑到：“直接说豆腐渣工程不就得了？”专家闻言急忙惶然离去。镜头一转，是一堆残存的砖墙，砖与砖之间几乎看不见水泥。<br />
<br />
我忘不了影片最后的村民们，对着镜头大声喊着党和政府的好处，并鄙夷地对一位失去孩子的母亲说：“你和他们乱说些什么，就告诉他们党和政府的好就行了。和这些外国人说什么！”那位母亲倍感委屈：“我什么时候说党和政府不好了，我一直都说他们好啊。人家就是来了解一下孩子的情况，你们家的孩子都好好活着，关你们什么事！”<br />
<br />
=================<br />
<br />
一年后，TVB《星期日档案》又拍下了22分钟的新闻调查《不能说的真话》，可以在线观看。这里记录的是一直在胡萝卜和大棒面前不屈不挠要为孩子讨还公道的父母，其中几位在《劫后天府泪纵横》里都出现过。这里也记录了深入灾区收集死亡孩子们的名单的公民记者们。这里头的故事，请阅读艾未未老师的博客。<br />
<br />
忘不了一位父亲转述的官员们安抚他们的话：“这次地震要是小，要是倒塌的学校没有那么多，要是死的孩子少的话，我们就把这事儿给你办了。可现在没法办啊，给你办了，其他人知道了，怎么办？只要你不到北京上访，不闹，有什么要求我们都能给你私底下解决，只给你一个人解决哦。但你要闹的话，那你就是反华组织。给个面子吧。”这位父亲说：“我孩子都死了，怎么给你面子啊。”<br />
<br />
忘不了一位抱着新出生的婴儿的母亲，她平静的说：“我们只是想知道真相，要个交待，直接告诉我们就好了啊，为什么非要从侧面跟我们说这些呢？不知道真相，我们怎么心安得了啊。”<br />
<br />
=============================<br />
<br />
忘不了这些，所以对今天所有腆颜出席各种悼念活动的大小官员，说一声：“悼念你妈逼啊！”<br />
<br />
===============================<br />
<br />
“地震一周年即将来临之际，四川省委宣传部常务副部长、省政府新闻办公室主任侯雄飞做客新浪访谈，他介绍说，共确定4个国家级地震遗址，将组织一些干部离岗疗养、考察、学习以减轻他们的心理压力。”<br />
<br />
