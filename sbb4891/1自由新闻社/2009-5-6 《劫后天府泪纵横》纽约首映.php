　　摄制组于5月22日到达成都，四人带着卫星电话，下了飞机还在当地配了手机，租用了两辆吉普车。他们从报道中选取了两个重灾区，夏明和Jon Alpert去绵竹市的汉旺镇，邝治中和Matt O’Neill去都江堰市的向峨乡。<br />
　　第一天（23日）：汉旺是绵竹的工业大镇，位于绵竹市正北偏西10公里处，人口有七八万之多。因为灾情严重，这里驻扎了很多军人。摄制组在汉旺拍摄到了片中第一个画面，一位小女孩走向倒塌的学校，向死在废墟里的同学鞠躬。<br />
　　第二天（24日）：听说绵竹市富新镇家长要游行到60里开外的成都请愿，倒塌的富新二小死了很多学生，家长们在废墟上搭设灵堂，花圈一直摆到了街上。摄制组立即转向富新，在路上遇见了游行队伍，Jon Alpert扛起摄像机混入游行队伍中，在都江堰的另一摄制组也火速赶来，Matt O’Neill拍摄外围。<br />
　　他们跟着行进了20多公里，拍摄了这样的画面：绵竹市委书记兼人大主任蒋国华下跪阻挡游行队伍，家长们和德阳市常务副市长张金明的对话，和德阳市副市长宋玉华的争吵，还拍摄到一位新华社女记者被当地官员驱赶。可能是摄制组的存在给游行家长们增加了安全感，家长们直叮嘱他们：请跟紧我们，请跟紧我们。 游行家长引来的官员中，有德阳市副市长兼公安局局长陈正权，他招来外事警察，四人被告知不允许拍摄游行。<br />
　　第三天（25日）：前一日巧遇家长游行，使他们决定聚焦于死难学生，这一天他们在德阳拍摄倒塌的学校。<br />
　　第四天（26日）：他们去死难学生在农村的家中做家访，采访顺利无人干预。<br />
　　第五天（27日）：听说汉旺中心小学家长们在堵路抗议，他们赶往拍摄，又遇上了德阳市副市长兼公安局局长陈正权，着令外事警察查验护照，发现他们持旅游签证，并无采访许可，这一天德阳市公安局发令，四人必须离开德阳地界，否则将遭逮捕。<br />
　　第六七八天（28、29、30日）：四人离开德阳，去都江堰、什邡和成都，走访拍摄了十多所学校。<br />
　　第九天（31日）：四人在成都听说德阳绵竹富新二小的家长们将在六一儿童节举行活动，为在天堂中的孩子们过节。这一活动非拍不可，这些画面将会为纪录片增色，但四人再去德阳，恐怕凶多吉少。于是他们做了第二手准备，雇请了一名当地摄影师，Jon Alpert给一台摄像机让他分开行动单独拍摄。<br />
　　第十天（6月1日）：四人坐着吉普刚到富新二小门口，就被警察包围。他们无法下车，只能放弃拍摄驱车回到成都。当地摄像师没被警察盯上，但在离开现场时被警察跟踪，一直尾随至成都，在成都兜了很大的圈子才甩掉警察，把录像带交给摄制组。由于当地摄像师坐的是夏明在成都姐姐家的私家车，警察径直前往夏明姐姐家搜查。 这天下午，四人前往成都一家超市购物，两位吉普车司机在停车场被警察扣留，警察从十多辆警车中冲下来把四人团团围住，后来他们才知道这是德阳警察和四川省公安厅出入境管理局的联合行动。 警察坚持要把四人押往四川省公安厅，他们拒绝，双方在停车场僵持。他们打电话给美国驻成都领事馆，被告知要避免警察冲突，于是便去了省厅，做了讯问和笔录，一直折腾到午夜十二点，共耗去8个多小时。因为夏明在四川有人际关系，警察没有搜查和扣留他们放在宾馆的拍摄素材。 四川省外办人员告诉他们：你们绝对违法，马上离开！国务院已下了新指令，六一以后，四川外办发的采访证全部作废，原来有证件的人都未必可以再采访，何况你们什么也没有，快走快走。<br />
　　第十一天（6月2日）：四人在上午乘机离开成都。<br />
　　6月23日至7月31日，回到纽约的夏明教授把58个小时录像中的四川话全部翻译成英文。8月，开始剪辑，最初成片一百分钟，后精剪为一个小时，Sheila Nevin将目标调整为角逐奥斯卡奖后，追加投资重新编辑，将未来市场扩大到电影院，并按奥斯卡要求，片长减为39分钟。<br />
　　被掩盖的豆腐渣<br />
　　四川地震共造成七千多所学校倒塌，中国公布的师生死亡人数是7千多人，封从德在震后的5月20日根据中国公开报道所做的统计是16365人。（见封从德：汶川师生遇难人数估算报告）<br />
　　摄制组认为中国政府在学校建筑质量问题上掩盖真相。根据他们在灾区所见，学校建筑倒塌比其他建筑多，富新小学的教学楼倒塌，旁边楼房却没倒，连门口一栋百年建筑都没倒，农民自建房也没有整体垮塌，而教学楼却结结实实的垮塌了。都江堰的新建小学教学楼也是一垮到底，周边楼房都没有垮。向峨小学全部垮塌，而旁边的医院纹丝不动，红白中学也是如此。<br />
　　在学校拍摄的时候，基层地方官员向家长们保证：我们一定查，一两个月就拿出报告，给大家一个说法，把罪人送上法庭。他们还拍摄到建筑专家在查看钢筋和混凝土后，指出明显不合格。<br />
　　原本说一两个月可以出的报告，后来一直没有出笼，也没有官员因学校倒塌受到处罚。人们看到的是政府用钱来解决问题，每个死去的孩子赔六万，再给家长存入三万元的社保。政治学教授夏明认为学校建筑质量问题牵涉很多官员的渎职和腐败，深挖下去，恐怕会挖垮官场这张网，四川地震会引起巨大的官场地震。“共产党通过政治上的分赃制度，通过机会、荣耀、物质和官职，来摆平下面的官员。官员又是通过同样的分赃，摆平党的积极分子和骨干分子，党的骨干分子又如法炮制在群众中寻找自己的骨干，如此这般一层层地建立起党的根基和体系。”夏明认为这才是捂盖子的根本原因。<br />
　　“学校出问题，绵竹和德阳的党委书记们和市长们恐怕脱不了干系，深查下去会触动党的基层官员和骨干分子的网络体系，所以党宁愿花钱解决问题，也不容许基层网络被触动。”夏明对多维分析说：“我认为捂盖子的决定是由中央作出的。”<br />
　　中国在地震中心<br />
　　十天在震区的拍摄，产生了一部准备角逐奥斯卡的《劫后天府泪纵横》，也为夏明的政治学研究带来了新的题目，他的新著《中国在地震中心》已经列入了哈佛大学出版社的出版计划。<br />
　　按照中国的历史逻辑，天灾必有饥荒，历来是灾荒相连，饥荒后出现流民，流民带来混乱直至起义。夏明在四川震区发现，共产党再次推翻了这一历史逻辑。1960年代的三年大饥荒，没有天灾却有饥荒，但却没有流民，更没有混乱，因为共产党强大的控制使得老百姓饿死都无法反抗。<br />
　　这次川震出现了另一个逻辑，有天灾，但没有饥荒，百姓得了高补偿还是要抗议闹事。夏明认为这是因为中国底层人民的根本需求，已经从过去对食品安全的追求，转向追求人身安全保障。这就是为什么，政府给他们发放了救济粮、抚恤金，他们还是要讨说法、讨公正。夏明对多维引述了纪录片中一位丧子母亲的话：钱，我不要！我们要记住这是一个血的教训，未来不能让它发生在其他人身上。<br />
　　夏明认为此话出自中国农民之口，说明中国百姓对人生的价值、尊严和安全的追求，超出了过去生活在饥饿和死亡边沿的人们对食品安全的追求。人们可以看出改革开放30年，中国人价值和尊严的提升。“这是中国最大的进步，但也是对中国共产党体制一个很根本的挑战，共产党认为你们吃饱了，为何还要放下筷子骂娘，因为共产党还在用食品安全的底线来要求中国人。”夏明对多维分析说。<br />
　　夏明在震区的第二个发现是：中国百姓的抗议没有激化成暴力冲突。他们拍摄了百姓对官员严厉的质问，甚至骂官员狼心狗肺，但冲突始终停留在言语层面，没有激化成暴力，他们甚至看出警察的心慌胆怯。夏明认为这是中国的进步，因为中国官员保持了极大的克制。在他们的亲身经历中，尽管不断遭受警察阻拦甚至扣留，他们发现警察的自我克制。<br />
　　夏明在《中国在地震中心》要阐明的另一观点是，中国政府在处理基层危机上的失败。在拍摄中他们发现，德阳富新二小家长们示威的成功激励了远在都江堰的新建小学的家长们，于是他们去堵路。夏明认为中国政府的危机处理方式是扬汤止沸，解决不了根本矛盾，结果是这边压下去，那边又起来了。<br />
　　豆腐渣学校问题至今没有解决，始终没给老百姓说法，夏明打电话回四川回访家长，年老一些的人会说：这就是天命，我们认了，不再想这些事情了。<br />
　　但火焰并无止熄，问题会接踵而至，别的地方矛盾爆发，这里的百姓也会趁机宣泄自己的委屈愤恨。夏明认为这就是为何在中国，一件小事就可以引发大规模的群体性事件，如贵州瓮安事件，一个女孩淹死，带出了计划生育被罚款者、被城管驱赶者、受过欺负者等等一系列人，人们把老账新账一起算。夏明认为这种扬汤止沸的危机处理模式，根本没有解决每一个细小的危机，这与中国政府要追求的和谐社会背道而驰，因为每个细节都埋伏着定时炸弹，一旦风吹草动，就酿成大危机。<br />
　　中国政府处理基层危机的失败还表现在《劫后天府泪纵横》中几个有趣的镜头：下跪的绵竹市委书记说“走，我也跟你们一起去，我也有很多冤屈。”一个镇长说“去，你们去跟上面说，让他们知道情况，也好帮助下面解决问题。”<br />
　　层层有冤屈，下情不上达，基层官员怕自己管不住下面，会被上级问罪。一旦基层问题控制不住，小官员们就有意把事情弄大，让上级知道此事重大棘手。夏明认为中国的底层官员有两个极端的选择，为了捂住问题，他们不择手段甚至下跪，一旦捂不住，他们就把事情夸大，让上级觉得不得了了革命了。小事变大事，基层官员一来可以推脱责任，二可引起上级注意。因为上级财政比基层充裕，可以用紧急财政拨款用钱来解决问题，于是地方官员得以解脱。<br />
