第九天（31日）：四人在成都听说德阳绵竹富新二小的家长们将在六一儿童节举行活动，为在天堂中的孩子们过节。这一活动非拍不可，这些画面将会为纪录片增色，但四人再去德阳，恐怕凶多吉少。于是他们做了第二手准备，雇请了一名当地摄影师，Jon Alpert给一台摄像机让他分开行动单独拍摄。<br />
<br />
第十天（6月1日）：四人坐着吉普刚到富新二小门口，就被警察包围。他们无法下车，只能放弃拍摄驱车回到成都。当地摄像师没被警察盯上，但在离开现场时被警察跟踪，一直尾随至成都，在成都兜了很大的圈子才甩掉警察，把录像带交给摄制组。由于当地摄像师坐的是夏明在成都姐姐家的私家车，警察径直前往夏明姐姐家搜查。 这天下午，四人前往成都一家超市购物，两位吉普车司机在停车场被警察扣留，警察从十多辆警车中冲下来把四人团团围住，后来他们才知道这是德阳警察和四川省公安厅出入境管理局的联合行动。 警察坚持要把四人押往四川省公安厅，他们拒绝，双方在停车场僵持。他们打电话给美国驻成都领事馆，被告知要避免警察冲突，于是便去了省厅，做了讯问和笔录，一直折腾到午夜十二点，共耗去8个多小时。因为夏明在四川有人际关系，警察没有搜查和扣留他们放在宾馆的拍摄素材。 四川省外办人员告诉他们：你们绝对违法，马上离开！国务院已下了新指令，六一以后，四川外办发的采访证全部作废，原来有证件的人都未必可以再采访，何况你们什么也没有，快走快走。<br />
<br />
第十一天（6月2日）：四人在上午乘机离开成都。<br />
<br />
6月23日至7月31日，回到纽约的夏明教授把58个小时录像中的四川话全部翻译成英文。8月，开始剪辑，最初成片一百分钟，后精剪为一个小时，Sheila Nevin将目标调整为角逐奥斯卡奖后，追加投资重新编辑，将未来市场扩大到电影院，并按奥斯卡要求，片长减为39分钟。<br />
<br />
被掩盖的豆腐渣<br />
<br />
四川地震共造成七千多所学校倒塌，中国公布的师生死亡人数是7千多人，封从德在震后的5月20日根据中国公开报道所做的统计是16365人。（见封从德：汶川师生遇难人数估算报告）<br />
<br />
摄制组认为中国政府在学校建筑质量问题上掩盖真相。根据他们在灾区所见，学校建筑倒塌比其他建筑多，富新小学的教学楼倒塌，旁边楼房却没倒，连门口一栋百年建筑都没倒，农民自建房也没有整体垮塌，而教学楼却结结实实的垮塌了。都江堰的新建小学教学楼也是一垮到底，周边楼房都没有垮。向峨小学全部垮塌，而旁边的医院纹丝不动，红白中学也是如此。<br />
<br />
在学校拍摄的时候，基层地方官员向家长们保证：我们一定查，一两个月就拿出报告，给大家一个说法，把罪人送上法庭。他们还拍摄到建筑专家在查看钢筋和混凝土后，指出明显不合格。<br />
<br />
原本说一两个月可以出的报告，后来一直没有出笼，也没有官员因学校倒塌受到处罚。人们看到的是政府用钱来解决问题，每个死去的孩子赔六万，再给家长存入三万元的社保。政治学教授夏明认为学校建筑质量问题牵涉很多官员的渎职和腐败，深挖下去，恐怕会挖垮官场这张网，四川地震会引起巨大的官场地震。&quot;共产党通过政治上的分赃制度，通过机会、荣耀、物质和官职，来摆平下面的官员。官员又是通过同样的分赃，摆平党的积极分子和骨干分子，党的骨干分子又如法炮制在群众中寻找自己的骨干，如此这般一层层地建立起党的根基和体系。&quot;夏明认为这才是捂盖子的根本原因。<br />
<br />
&quot;学校出问题，绵竹和德阳的党委书记们和市长们恐怕脱不了干系，深查下去会触动党的基层官员和骨干分子的网络体系，所以党宁愿花钱解决问题，也不容许基层网络被触动。&quot;夏明对多维分析说：&quot;我认为捂盖子的决定是由中央作出的。&quot;<br />
十天在震区的拍摄，产生了一部准备角逐奥斯卡的《劫后天府泪纵横》，也为夏明的政治学研究带来了新的题目，他的新著《中国在地震中心》已经列入了哈佛大学出版社的出版计划。<br />
<br />
按照中国的历史逻辑，天灾必有饥荒，历来是灾荒相连，饥荒后出现流民，流民带来混乱直至起义。夏明在四川震区发现，共产党再次推翻了这一历史逻辑。1960年代的三年大饥荒，没有天灾却有饥荒，但却没有流民，更没有混乱，因为共产党强大的控制使得老百姓饿死都无法反抗。<br />
<br />
这次川震出现了另一个逻辑，有天灾，但没有饥荒，百姓得了高补偿还是要抗议闹事。夏明认为这是因为中国底层人民的根本需求，已经从过去对食品安全的追求，转向追求人身安全保障。这就是为什么，政府给他们发放了救济粮、抚恤金，他们还是要讨说法、讨公正。夏明对多维引述了纪录片中一位丧子母亲的话：钱，我不要！我们要记住这是一个血的教训，未来不能让它发生在其他人身上。<br />
<br />
夏明认为此话出自中国农民之口，说明中国百姓对人生的价值、尊严和安全的追求，超出了过去生活在饥饿和死亡边沿的人们对食品安全的追求。人们可以看出改革开放30年，中国人价值和尊严的提升。&quot;这是中国最大的进步，但也是对中国共产党体制一个很根本的挑战，共产党认为你们吃饱了，为何还要放下筷子骂娘，因为共产党还在用食品安全的底线来要求中国人。&quot;夏明对多维分析说。<br />
<br />
夏明在震区的第二个发现是：中国百姓的抗议没有激化成暴力冲突。他们拍摄了百姓对官员严厉的质问，甚至骂官员狼心狗肺，但冲突始终停留在言语层面，没有激化成暴力，他们甚至看出警察的心慌胆怯。夏明认为这是中国的进步，因为中国官员保持了极大的克制。在他们的亲身经历中，尽管不断遭受警察阻拦甚至扣留，他们发现警察的自我克制。<br />
<br />
夏明在《中国在地震中心》要阐明的另一观点是，中国政府在处理基层危机上的失败。在拍摄中他们发现，德阳富新二小家长们示威的成功激励了远在都江堰的新建小学的家长们，于是他们去堵路。夏明认为中国政府的危机处理方式是扬汤止沸，解决不了根本矛盾，结果是这边压下去，那边又起来了。<br />
<br />
豆腐渣学校问题至今没有解决，始终没给老百姓说法，夏明打电话回四川回访家长，年老一些的人会说：这就是天命，我们认了，不再想这些事情了<br />
但火焰并无止熄，问题会接踵而至，别的地方矛盾爆发，这里的百姓也会趁机宣泄自己的委屈愤恨。夏明认为这就是为何在中国，一件小事就可以引发大规模的群体性事件，如贵州瓮安事件，一个女孩淹死，带出了计划生育被罚款者、被城管驱赶者、受过欺负者等等一系列人，人们把老账新账一起算。夏明认为这种扬汤止沸的危机处理模式，根本没有解决每一个细小的危机，这与中国政府要追求的和谐社会背道而驰，因为每个细节都埋伏着定时炸弹，一旦风吹草动，就酿成大危机。<br />
<br />
中国政府处理基层危机的失败还表现在《劫后天府泪纵横》中几个有趣的镜头：下跪的绵竹市委书记说&quot;走，我也跟你们一起去，我也有很多冤屈。&quot;一个镇长说&quot;去，你们去跟上面说，让他们知道情况，也好帮助下面解决问题。&quot;<br />
<br />
层层有冤屈，下情不上达，基层官员怕自己管不住下面，会被上级问罪。一旦基层问题控制不住，小官员们就有意把事情弄大，让上级知道此事重大棘手。夏明认为中国的底层官员有两个极端的选择，为了捂住问题，他们不择手段甚至下跪，一旦捂不住，他们就把事情夸大，让上级觉得不得了了革命了。小事变大事，基层官员一来可以推脱责任，二可引起上级注意。因为上级财政比基层充裕，可以用紧急财政拨款用钱来解决问题，于是地方官员得以解脱。<br />
<br />
<br />
--------------------------------------------------------------------------------<br />
<br />
汶川地震给中国社会带来了什么进步？<br />
<br />
BBC点评中国李大同／5.12大地震一周年了。比起地震时中国所有媒体铺天盖地的报道，如今灾区的人们怎么样了，已很少有报道。中国人真的是一个健忘的人类共同体吗？如果只从媒体报道上来衡量，一定会得出肯定的结论。<br />
<br />
拒绝遗忘<br />
<br />
然而我们知道有一群人正在做着媒体不予报道或者被禁止报道的事儿，那就是要调查出并向社会公布地震中所有死亡孩子的名单。<br />
<br />
发起这项调查的人是艺术家艾未未，响应他的提议的志愿者有数百名，他们拒绝遗忘。<br />
<br />
人已经死了，是否知道他们的名字有意义吗？很有意义。有名字，死者就是具体的、有音容笑貌的，可追忆的，可尊重的。<br />
<br />
没有名字，死者就只是一个抽象的数字，冷酷的，几乎没有任何意义。拍摄电影《南京，南京》的导演陆川近日披露，他在影片中，力图使被日军屠杀的同胞具体起来，于是要求数名研究这段历史的国内专家提供一些具体的名字，这个小小的要求竟然难住了这些专家。<br />
<br />
相比之下，日本在原子弹轰炸中死去的十几万人，每一个人都有名有姓，连婴儿也不例外。以色列的大屠杀纪念馆里，更是挂满了成千上万在集中营里被屠杀的死者的生前照片。<br />
<br />
亡灵就这样&quot;活着&quot;，震撼人心。生者对死者的态度，标示着人类的文明程度。<br />
<br />
&quot;公民调查&quot;受阻<br />
<br />
正是在这个意义上，艾未未们称为是&quot;公民调查&quot;的行动，也是文明之举。他们深入灾区，采访了二三百个遇难者的父母或亲戚，让他们谈了在什么地方听说地震，地震时他们是什么状态，是什么时候知道自己的孩子遇难了，当时的反应是什么，后来到学校以后的救助情况是什么，包括提取DNA、掩埋，政府的安抚工作，包括现状。<br />
<br />
这些都以纪录片的方式留存下来，这里面&quot;问题太多了&quot;。笔者相信，这些调查记录，将是一份可贵的人文资源。可惜，这些热心人不受政府欢迎。<br />
<br />
我看到不少志愿者赴震区调查时的记录，几乎每个人都受到当地警察的跟踪、讯问和遣返，警察问得最多的是：第一，你们是什么目的；第二，你们为什么要拿到这个名单？第三，你们后面是什么样的组织，谁资助你们？很明显，对这些志愿者的拦截是由当地政府统一布置下来的。<br />
<br />
在法理上，应当承担调查、公布死亡公民数字、名单责任的，恰恰是当地政府。当地政府在干什么呢，据报道是要投资几十亿建设一个什么地震遗址公园，成为旅游开发的重要项目，引发网民们的强烈质疑。<br />
<br />
一年的时间过去了，至少可以公布第一批死亡者的名单吧？做这个事儿很难吗？艾未未们在层层阻拦之下，仍迅速确定了四千多死亡孩子的姓名。可当地政府官员称这是&quot;国家机密&quot;。<br />
<br />
笔者看到这些报道常常感到困惑，为什么在民众看来是善举的事情，到了政府官员那里就成了一个&quot;阴谋&quot;？<br />
<br />
真相<br />
<br />
实际上，有不可告人之处的，恰恰是某些政府官员的作为。一位署名&quot;耀华&quot;的志愿者，在经过一个月的调查后，坦承&quot;接近真相的过程像是一次次被强奸的过程&quot;，&quot;越接近真相就越绝望&quot;。<br />
<br />
&quot;耀华&quot;能够接触到的真相肯定极其有限，即便如此，仍然令人震惊：&quot;一位大妈告诉我们，政府把救灾物资存放在库房里不予下发，有一次领导要来视察，政府来不及处理，雇了很多村民把大批的衣服和大米拉到外面烧掉，由于太多，一些来不及烧掉的就挖坑填埋。这位大妈是当时被雇去做这些事情的人之一。&quot;<br />
<br />
类似的事情屡屡发生，一次村民们在晚上十点钟，突然被召集领取某项救灾款，后来才知道款早就拨了下来，第二天上面有领导专门来视察。<br />
<br />
一位大哥说他们一次在新闻里看到说外面给映秀捐赠了一万件羽绒服，但是他们从来没见到衣服的影子，他说映秀就剩下三千来人了，那一万件羽绒服到哪里去了。&quot;<br />
<br />
问题远不止这些，譬如还有志愿者了解到捐赠给灾民的板房甲醛严重超标。如果媒体有报道自由的话，将可以发现救灾中的种种问题，可以讨论解决的方法，可以形成今后应对灾难的财富。<br />
<br />
可惜，在官员们看来，所有这些问题都是&quot;国家机密&quot;。一年前信誓旦旦要严查豆腐渣学校工程的官员，现在不查了，一律推给地震，而不容否认的事实是，震区那些严格监督下施工的数十所&quot;希望小学&quot;，没有一所坍塌，甚至完好如初。数百亿元慈善捐款究竟用去了多少，怎么用的，只有天知道。<br />
<br />
地震一周年前夕，北川县委宣传部副部长冯翔自杀了。留下了充满悬疑的博文：&quot;我的存在，是他们的恐惧，是他们的对手，一个对手的离去，对于他们，是多么值得庆贺的事情啊！&quot;&quot;告诉您，不要逼我，真的，不要逼我。好不好？&quot;谁是冯翔的&quot;对手&quot;，谁在&quot;逼&quot;他做什么？<br />
<br />
笔者遗憾，在地震中被激发出来的中国民众的善心、善举，没有能固化为制度成果；近10万生命的消失的代价，究竟给中国社会带来了什么进步？如果没人能说出来，那是谁之过？<br />
<br />
