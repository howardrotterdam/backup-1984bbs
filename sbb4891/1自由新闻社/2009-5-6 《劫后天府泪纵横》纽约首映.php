<br />
纪录片《劫后天府泪纵横》5月4日在纽约作全球首映。影片记录了四川绵竹富新小学的地震受难学生的家长们在地震后为死去孩子讨还公道的抗争。该校有127名学生在地震中因校舍坍塌而死亡。<br />
<br />
<br />
<img src="http://i43.tinypic.com/dpcv0p.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
2008年六月一日四川绵竹市富新第二小学遇难学生家长的抗议图片<br />
（本台特约记者曹国兴提供照片）<br />
<br />
这部纪录片由纽约市立大学亨特学院华裔教授邝治中、记录片制作人阿尔帕特、曾3次获得艾美奖的制片人奥尼尔和纽约市立大学史太登道学院政治学教授夏明在美国家庭影院影院频道（HBO）赞助下完成。<br />
<br />
邝治中说，他们于去年5月21日抵达四川德阳，原本为了拍摄灾后救援，结果遇到富新小学受难学生家长游行。影片记录了死难学生家长发现震塌的校舍建筑偷工减料，决心为死去的孩子讨还公道，他们手持镶着死难孩子照片的镜框游行到上级市政府，要求对豆腐渣工程作出调查，然后官员和警察前来阻拦，发生警民冲突，其中还包括了已被广为报道的绵竹市委书记蒋国华下跪央求游行家长不要前往德阳市的场面。虽然在家长们游行时地方政府信誓旦旦说一定进行调查，但最后却不了了之。<br />
<br />
邝治中教授说，因为中国政府宣布开放记者采访所以他们结队前往。但几天后当地政府就开始对外国记者进行干扰，要求受难者家属不要接受采访，指这是破坏国家形象，最后在他们决定离开前更遭安全部门拘禁。由于摄制组四人均持美国护照，被问话8个小时后获释。<br />
<br />
在首映式后的问答会上，许多观众对影片真实生动的反应地震死难家属的悲痛和激愤，对家长们为孩子的尊严所进行的抗争印象深刻。邝治中说，汶川地震一周年即将来临，但中国政府至今把地震中学生的死亡人数当作国家机密，也没有对死难学生家长指控的豆腐渣校舍进行调查。据报道，这部纪录片已送2009年奥斯卡短纪录片参选。<br />
