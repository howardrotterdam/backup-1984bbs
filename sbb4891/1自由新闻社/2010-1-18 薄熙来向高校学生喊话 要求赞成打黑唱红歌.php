													<h2>薄熙来向高校学生喊话 要求赞成打黑唱红歌</h2>
						
						
																											<div id="postmessage_437905" class="t_msgfont">薄熙来：有些人埋怨我们“打黑” 自己不干事<br />
2010年01月18日 07:13来源:重庆晚报<br />
市领导与参加座谈会的师生代表合影。重庆日报图<br />
<br />
16日上午，雨后初晴，重庆大学A区主楼101会议室气氛热烈，市委书记薄熙来、代市长黄奇帆等市领导坐在同学们中间，与来自全市27所高校的近300名学生代表亲切交流。<br />
<br />
森林重庆<br />
<br />
关于碳排放，重庆做得很模范，我们一年种了10年的树，今后10年恐怕要种100年的树<br />
<br />
重庆大学赵旭文绉绉地问：“哥本哈根会议未能达成实质协议，因为经济发展与温室气体减排存在矛盾，但世界进入低碳时代，中国已提出减少二氧化碳排放，这会不会影响重庆未来发展？”<br />
<br />
薄熙来直截了当地说：“讲到碳排放，咱重庆做得很模范，因为我们一年种了10年的树，今后10年恐怕要种100年的树！”<br />
<br />
薄熙来说，咱重庆人守着长江母亲河，就是要好好尽孝心，在沿江多种树，保持水土，建成名副其实的“森林重庆”。<br />
<br />
打黑除恶<br />
<br />
有些人埋怨我们“打黑”，自己不干事，对人民群众拥护的事，又酸溜溜地说三道四<br />
<br />
西南政法大学三年级的姚一博抢到了发言权：“平安重庆是根本，我们希望打黑除恶一定要坚持下去！”<br />
<br />
薄熙来问大家：“打黑除恶你们赞成不赞成？”<br />
<br />
全场300多位同学声音响亮有力：赞成！<br />
<br />
薄熙来说，一个地方的发展必须有正气。“打黑除恶”就是树正气，让老百姓安居乐业。如果一个地方路霸、肉霸、车霸、矿霸……猖獗，那老百姓还怎么过日子？他们大多数都是小家小户，居家过日子不容易，如果再遇到这样那样的干扰，老百姓的生活就会更困难。我们党和政府必须主持公道，为老百姓提供一个安全的、有保障的生活环境。有些人埋怨我们“打黑”，其实做任何事情，总会有一些人品头论足、七嘴八舌，总会有《愚公移山》中“智叟”式的人物，自己不干事，对人民群众拥护的事，又酸溜溜地说三道四，东拉西扯。如果有道理，我们就听，如果是歪道理，我们就不能听。党和政府必须头脑清醒，坚定不移地把打黑除恶进行到底。<br />
<br />
红色文化<br />
<br />
重庆人不能丢了红岩精神，不能忘了“红梅赞”<br />
<br />
四川外语学院三年级学生付予说，大家都喜欢“唱读讲传”，我们用8个语种诵读《共产党宣言》。相对于单纯的说教，诵读红色经典，生动活泼，更能激发我们立大志，为国家民族兴旺担当责任。学生们还强烈希望市委、市政府把红色经典文化活动坚持下去，用制度化的形式发扬光大。<br />
<br />
薄熙来又问大家：“唱红歌，读经典，大家喜不喜欢？”<br />
<br />
全场齐声响应：喜欢！<br />
<br />
薄熙来说，红色文化，说到底，就是共产党人一直坚持的，有中国特色的社会主义文化。有的人附庸“风雅”，觉得自己比较“洋气”，比较“时髦”，对革命文化嗤之以鼻。我们搞“唱读讲传”，他却在旁边评头品足，不屑一顾，有的还说我们在“搞运动”，是“左的一套”……<br />
<br />
重庆是一个有光荣革命传统的城市，我们不能忘记了英雄城市的光荣传统。我们小时候读《红岩》，就被感动得落泪。重庆人尤其不能丢了红岩精神，不能忘了“红梅赞”。<br />
<br />
城市精神<br />
<br />
要用先进的、正派的东西，抵制那些低俗的东西，以改善重庆的文化环境<br />
<br />
薄熙来说，中医有个理论，叫扶正祛邪，这是就人的调理而言，社会治理和精神建设也情同此理。全市开展社会治安综合治理，扫黄打非，打黑除恶，就属于“祛邪”；我们弘扬社会主义精神文明，“唱读讲传”，评选先进模范人物，就是“扶正”。既要“祛邪”，也要“扶正”。要以正压邪，用先进的、正派的东西来抵制那些低俗的东西，不断改善重庆的文化环境，为青少年的成长创造良好的氛围。 <br />
<br />
薄熙来说，“唱红”与“打黑”都是立足于培育重庆的精神素质，“唱红”是为提升重庆的精气神，提高重庆人的文化和思想素质；“打黑”就像大扫除，下决心清除城市的污泥浊水，还城市一片蓝天和净土。<br />
<br />
古文经典<br />
<br />
青年人对古文经典可择其精华，下决心背出二三十个长段，今后就可运用自如，终生受益<br />
<br />
薄熙来问同学们，诵读经典时，是出于压力，还是自觉自愿？背诵过程中，是否发自内心的感动，受到教育？同学们说：“我们是全神贯注来诵读这些经典名篇的，这让我们对生活、对人生有了更多的理解和认识。”<br />
<br />
薄熙来还与大学生交流学习体会。他说，年轻人朝气蓬勃，每天都接触到大量新知和信息，这是优势，但往往读的东西多，浏览的多，有用的东西不一定很多。如果大量浏览而不深入思考，就容易肤浅。青年人需要读好书，首先要选好书，要静下心来读。年轻人记忆力好，对古文经典可择其精华，下决心背出二三十个长段，今后就可运用自如，终生受益。<br />
<br />
人生意义<br />
<br />
我们能够在这个环境里和平地生活与工作，已经很幸运了，就不要再有非分之想了，为百姓多干一点好事就是一种幸福<br />
<br />
解放军后勤工程学院学生毛育文，连珠炮似的提了3个问题：<br />
<br />
“薄书记，您觉得人生的意义是什么？”<br />
<br />
薄熙来：“那当然是为人民服务了。<br />
<br />
“您觉得最快乐的事情是什么？”<br />
<br />
薄熙来：“那还用说吗？把我们的重庆建设好。”<br />
<br />
“您觉得最痛苦的事情是什么？<br />
<br />
薄熙来：“一个道理，那就是没把咱重庆的工作做好。”<br />
<br />
同学们用热烈的掌声，回应这三个最简单、最直白的回答。<br />
<br />
薄熙来说，我们生长在这片土地上，重庆就是我们共同的家园。为了这个家园，很多人抛头颅洒热血。像杨公烈士、江姐，他们很年轻就牺牲了。我们能够在这个环境里和平地生活与工作，已经是很幸运了，就不要再有非分之想了，为百姓多干一点好事就是一种幸福！<br />
<br />
寄语青年<br />
<br />
未来中国能不能继续走正道、有后劲，能不能稳定强健地发展下去，更上一层楼，就看青年一代有没有健康向上的精神和科学发展的本领了<br />
<br />
薄熙来说：“看着你们这一代，让人有一种历史的感觉。想到毛主席、邓小平等老一代无产阶级革命家，他们很伟大，很了不起。以江泽民、胡锦涛为代表的中国共产党人，继续坚持改革开放，以“三个代表”和科学发展观为指导，使我国国力迅速提升，取得了伟大的成就。现在中国的外汇储备突破了2万亿美元，居世界榜首。去年中国的出口也超过了德国，跃居世界第一。我国在经济上已昂首挺胸、站在了新的平台，西方已不得不对我刮目相看。<br />
<br />
