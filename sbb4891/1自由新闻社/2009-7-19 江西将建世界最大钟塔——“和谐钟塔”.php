													<h2>江西赣州耗资2.9亿建全球最大“和谐钟塔”，为祖国60年大庆送钟</h2>
						
						
																											<div id="postmessage_169193" class="t_msgfont">新闻出处：赣州信息港 <a href="http://gz.jxcn.cn/news/2009-7/200971073621.htm" target="_blank">http://gz.jxcn.cn/news/2009-7/200971073621.htm</a><br />
集天地大观塔雄盛世福延万代得山水灵气钟鸣和谐史炳千秋<br />
赣州和谐钟塔昨开建<br />
<br />
潘逸阳宣布开工王平致辞陈春明王昭悠赖联明等为工程项目培土奠基<br />
<br />
<br />
<br />
7月9日，赣州和谐钟塔开工奠基仪式在章贡区水东镇虎岗村举行。记者郭智勇摄<br />
<br />
7月9日上午，在风光秀丽的赣江之滨DD章贡区水东镇虎岗村赣州大桥东岸引桥南侧，赣州新的标志性景观、当今世界钟塔建设的又一杰作DD赣州和谐钟塔正式开工建设。<br />
<br />
省委常委、市委书记潘逸阳宣布赣州和谐钟塔开工，市委副书记、市长王平致辞，并和市领导陈春明、王昭悠、赖联明、骆炳峰、林泽华、尹礼山、欧阳世麟等一起为赣州和谐钟塔培土奠基。林泽华主持开工奠基仪式。<br />
<br />
在喜庆热烈的气氛中，王平在开工奠基仪式上致辞。他说，赣州和谐钟塔项目是未来赣州新的标志性景观，是提升我市城市品位、造福我市百姓的民心工程，也是我市扩大对外开放的窗口工程。项目的开工建设，是落实市委、市政府“对接长珠闽、建设新赣州”发展战略，拉大城市框架，增强集聚辐射能力，彰显城市魅力，把赣州建设成为区域性现代化中心城市的又一重要举措。她的建成，将对我市进一步完善城市功能，优化发展环境，加快中心城区现代化建设，推进城市化进程，实现赣州经济社会又好又快发展产生积极而深远的影响。<br />
<br />
王平说，赣州和谐钟塔的顺利开工，离不开项目建设有关单位和部门的积极配合与大力支持，离不开参与项目建设的同志们不辞辛劳地奔波协调，特别是离不开逸阳书记的高度重视。从项目的规划论证到钟塔的设计开工，逸阳书记多次听取汇报，作出批示，提出要把和谐钟塔建设成为体现赣州地域特色及创新精神，象征赣州今后繁荣富强的百年经典建筑、千年传世精品。项目业主和参建单位要按照逸阳书记的要求，围绕打造“精品工程”、“平安工程”、“廉洁工程”和“满意工程”的目标，科实施项目管理，精心组织施工。各有关单位和部门要积极配合搞好服务，创造良好的建设环境。相信通过全体参建者的共同努力，赣州和谐钟塔一定能够与赣州大桥在2010年6月底前同步竣工，为构建四省通衢区域性现代化中心城市添上画龙点睛的一笔。（赖锦洪、记者李国生）<br />
<br />
阅读延伸：<br />
<br />
赣州和谐钟塔是一个以观光钟塔为核心，由城市景观绿地、历史文化陈列区、欧式风情街区、接待中心区和商住区所构成的集文化性、开放性于一体的综合主题公园项目。共分两部分：一部分为欧式主题公园，设计有钟塔、配套用房、接待中心、欧式风情街、中心广场和绿化区；另一部分为商住区，设计有办公、商业、会所及独栋、联排别墅和多层住宅等花园小区。项目总用地面积485亩，建设投资估算 2.9亿元。和谐钟塔高113米，外观9层，与玉虹塔隔江相望，立面采用现代欧式风格，内设陈列、展览、“时间长廊”、观光平台和旋转餐厅等。钟楼将安装目前世界上最大的机械塔钟，表盘直径达12.8米，超过世界上最著名的英国大本钟。主题公园分时区设立古今中外时间、天文方面著名的科家塑像，塔钟将在每天太阳升起时报时一次，白天每半小时报时一次，整点按时点响钟。<br />
