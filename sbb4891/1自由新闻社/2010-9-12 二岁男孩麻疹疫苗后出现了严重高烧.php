&nbsp; &nbsp; 针对脊髓灰质炎（即小儿麻痹症）的疫苗强化免疫活动，更是在全国、全省范围内经常进行，世界卫生组织还为此在1988年设立了世界强化免疫日（每年的12月15日）。<br />
<br />
事实：WHO在1988年才首次提出消灭脊灰，其相关文件中没有什么“世界强化免疫日”。其支持者指点我说，那是“World Strengthened Immunization Day”，这一词汇文法不通。方舟子还煞有其事地介绍每年12月15日是“世界强化免疫日”，真能扯。<br />
<br />
二、方舟子口口声声重复“强化免疫”：<br />
<br />
&nbsp; &nbsp; 我才注意到卫生部决定于2010年9月在全国范围开展适龄儿童麻疹疫苗强化免疫活动<br />
<br />
&nbsp; &nbsp; 这次麻疹疫苗强化免疫计划，不是因为有疫情，而是为了实现全国消除麻疹的目标。<br />
<br />
&nbsp; &nbsp; 在这种情况下，进行强化免疫运动就显得很有必要。<br />
<br />
&nbsp; &nbsp; 2000年至2008年期间，共有136个国家和地区开展了407次麻疹疫苗强化免疫活动<br />
<br />
&nbsp; &nbsp; 美洲各国早在1989～1995年期间进行了麻疹疫苗强化免疫活动<br />
<br />
&nbsp; &nbsp; 这也不是中国第一次进行强化免疫活动。<br />
<br />
&nbsp; &nbsp; 自2004年以来，陆续有27个省开展了麻疹疫苗强化免疫活动。<br />
<br />
&nbsp; &nbsp; 因此世界卫生组织的建议是在进行强化免疫时不考虑是否已有免疫史。<br />
<br />
&nbsp; &nbsp; *********<br />
<br />
谎言重复数遍，大家就信了。既然方舟子口口声声提世界卫生组织，我们看看WHO的相关文件。针对麻疹“立场文件”，WHO提供了中文版：<a href="http://www.who.int/immunization/documents/Measles_Chinese-updated_May_2010.pdf" target="_blank">http://www.who.int/immunization/ ... pdated_May_2010.pdf</a><br />
<br />
在第5页上，接种策略一节中，WHO称类似的计划为“补充免疫活动”（SIAs，英文为Supplementary Immunication Activities）。<br />
<br />
针对“强化”，还是“补充”，这两个词词义大有区别，因为补充者，你没有免疫够次数才需要补充，而强化，听起来好象始终有好处似的。在免疫学中，强化与加强是有特殊含义的，比如乙肝免疫，第一次接种以后，加强或者强化，会诱导更强烈的免疫反应，针对即使第一次产生了免疫应答的人来说，都是有价值的。<br />
<br />
比如WHO与联合国儿童基金会的共同宣言有如下说法：<a href="http://whqlibdoc.who.int/hq/2005/WHO_IVB_05_11_eng.pdf" target="_blank">http://whqlibdoc.who.int/hq/2005/WHO_IVB_05_11_eng.pdf</a><br />
<br />
&nbsp; &nbsp; virtually all older children who receive a second opportunity for measles immunization vaccination, delivered either through routine immunization services or periodic supplementary immunization activities (SIAs), are completely protected for life against measles. （page 1)<br />
<br />
&nbsp; &nbsp; 差不多所有的接受第二针免疫的大孩子，不管是通过计划接种，还是定期补充免疫活动，都得到终生对麻疹的保护。（第一页）<br />
<br />
补充，不言而喻，是针对孩子的两针计划而去的。而强化，就可以不管你接种了多么次。<br />
<br />
三、方舟子重复WHO专家的谎言：<br />
<br />
&nbsp; &nbsp; 例如，美洲各国早在1989～1995年期间进行了麻疹疫苗强化免疫活动和每4年进行1次后续麻疹疫苗强化免疫活动，2002年11月以来，美洲未发现有本土麻疹病毒传播。<br />
<br />
我早就驳斥过WHO的专家，举1994年英国补充免疫是不适当的，因为当时英国计划免疫只有一针。方舟子搬来的美洲经验，只不过是英国经验的拷贝而已。这些国家计划接种只种一次，当然可以实行普种了！因为谁都没有接种二次的免疫史。而中国需要的是香港1997年的经验，此时，麻疹接种二次已成为常规，而香港就避开了已经接种了两次的孩子，不让他们打第三针，最重要的是，香港取得了成功。<br />
<br />
我们还需要吸收同样在非洲取得成功的麻疹补种计划，非洲大多也只有一针计划免疫，所以可以搞不管接种史的普种，但WHO的非洲地区行动指南就明确提出了补充免疫计划的目标：<br />
<br />
&nbsp; &nbsp; Supplemental immunization activities are necessary to reach never-vaccinated children who have never had measles disease and to provide an opportunity for a second dose for cases of primary vaccine failure.（First sentence in Purpose of SIAs, page 7)SIA针对从未接种也从未得过麻疹以及那些首次接种失败的人提供第二次机会是必要的。（第七页，补充免疫活动目标第一句话）<br />
<br />
我们回到WHO的立场文件：<br />
<br />
&nbsp; &nbsp; 麻疹疫苗的接种方针和服务提供策略在各国差异很大。“扩大免疫规划”初期曾建议仅接一针种MCV。不过，如果仅在9月龄接种一针麻疹苗，原发性免疫失败率可高达10%~15%，因此该接种策略已被证实并不足以防止麻疹暴发。截至2008年，WHO 193个会员国中已有192个国家采纳了接种2针的策略。共有针次疫苗132个国家常规接种2针次，其中49个国家定期开展全国性免疫接种活动，即所谓的“补充免疫活动” (SIAs)；39个国家 开展一次性的初始强化免疫；44个国家完全依靠常规免疫规划提供2针次麻疹疫苗。共有60采取常规接种第1针、再定期开展SIAs的策略。（第五页，接种策略，第一段）<br />
<br />
相信稍有阅读理解能力的人就能断定WHO推荐二针策略。<br />
<br />
四、方舟子把疫苗当饭吃了：<br />
<br />
&nbsp; &nbsp; 曾接种过麻疹疫苗的儿童可以再接种一剂或多剂麻疹疫苗而不会对身体健康造成损害。<br />
<br />
方舟子把逻辑自洽性（Integrity）译为完整性是有预谋的，如果我批评方舟子没有完整性，他就象这次晒屁股一样，把衣服一脱，哪样零件都不缺，于是乎有了完整性！一个自称科学的人，怎么敢如此胡说八道？所有的疫苗都有副作用，最起码，针扎一下不痛么？一点生活常识都没有，而如果要科普，岂能不接触相关文献，有哪个文献宣称疫苗绝对无害而不提及副作用的？<br />
<br />
方舟子不承认疫苗有副作用，直接宣称寻正造谣好了，但他却无知地来批评寻正针对麻疹疫苗副作用的说法有误：<br />
<br />
&nbsp; &nbsp; 美国一所医学院的财务分析师廖俊林以寻正为名，以公共卫生专家的身份教训世界卫生组织专家，写了《中国2010年普种麻疹疫苗问答录》，谎话连篇，例如谎称注射麻疹疫苗“不到万分之1发生严重过敏反应，低于百万分之1的人发生脑炎脑病”。其实前者发生率不到百万分之一，后者未能确定与疫苗有关。（方舟子微博）<br />
<br />
方舟子在指责寻正的时候，就没想到自己已经否决了疫苗无害论。哎，要是松鼠会不做科普了，寻正闭嘴了，方舟子一个人唱科学大戏多好啊？<br />
<br />
我没兴趣详细列举很多的例证来说事儿，举点有说服力的就行了。方舟子也抄过的WHO立场文件上有专门一节谈不良反应：注射部位疼痛与压痛，可伴低热与局部淋巴结肿大；高达5%的人出现高热，超过39.4摄氏度，持续1-2天。高热惊厥发生率1/3千，2%出皮疹，血小板减少性紫癜1/3万。过敏反应1/10万。<br />
<br />
前面提及的WHO非洲办公室行动指南列举麻疹疫苗不良反应：高热（&gt;39.4）1/9；皮疹1/10；高执惊厥1/2500；脑炎脑病1/百万；亚急性硬化性全脑炎1/百万。<br />
<br />
对于我的数据，我举一例足矣，1987年柳叶刀杂志一篇文章总结，麻疹疫苗过敏性休克9/17万。面对中国缺乏市场后不良反应监测，对副作用我当然依大不依小，以保证提供的数据准确性（不小于1/万可以包括1/10万或者更小的数据）。<br />
<br />
五、方舟子自相矛盾：<br />
<br />
&nbsp; &nbsp; 这是指在短时间内统一对易感人群注射疫苗，让足够多（至少95%）的人产生了抗体，才能形成免疫屏障，阻断病毒传播。<br />
<br />
&nbsp; &nbsp; 强化免疫活动必须要有足够多（95％以上）的易感人群接受了接种并产生了抗体才能成功<br />
<br />
好吧，如果只要95%的人群屏障，一次接种就够了！<br />
<br />
&nbsp; &nbsp; 我的小孩虽然此前已接种过两次含麻疹疫苗的麻风腮疫苗，有99％的可能已对麻疹免疫，但是我还是要根据要求让其再接种一次。<br />
<br />
既然只要95%就够了，为什么到99%还要打第三针呢？第三针有什么效果？<br />
<br />
事实上，国内专业人员已经跟我相互印证了，中国的麻疹断而不绝，是因为年青成人（Young Adults）免疫力不足的缘故。你的孩子99%的几率免疫了，再打一针白打，为什么不把这一针节约给那些可能只有50%几率有免疫力的人呢？如果方舟子孩子打了两针麻风腮，假如都是1岁以后打的，那么每一次免疫失败的几率是2-5%，合并为0.04-0.25%的失败率。第三针成功的几率是 0.038-0.245%！完全可以忽略不计。<br />
<br />
六、方状元的奇怪逻辑：<br />
<br />
&nbsp; &nbsp; 有人觉得奇怪，家中儿童已经接种过一次甚至两次麻疹疫苗，为什么还要求也一起再接种一次？这是因为麻疹疫苗接种后，仍有一小部分人免疫不成功。免疫不成功的人群虽然比例很小，但是累积到一定程度就可能导致麻疹局部暴发。<br />
<br />
作为语文状元的方舟子因为没人教逻辑，经常出现无法理解的逻辑，比如这里的“比例累积”，我想请教方语文状元，很小的比例如何积累？想起小学的动脑筋习题了：<br />
<br />
&nbsp; &nbsp; 问：北京人的免疫失败率为5%，上海人的免疫失败率为4%，北京加上海人加在一起免疫失败率是多少？<br />
<br />
&nbsp; &nbsp; 三毛：9%！（鼓掌）<br />
<br />
七、方舟子懂我国麻疹疫情么？<br />
<br />
&nbsp; &nbsp; 我国相应地制定了《2006～2012年全国消除麻疹行动计划》，但是这几年来我国麻疹报告发病率都接近10/10万，与世界卫生组织提出的消除麻疹目标尚有较大差距。<br />
<br />
数据最易说话：<br />
 &nbsp; &nbsp; &nbsp; &nbsp; 麻疹发生率/10万<br />
2005&nbsp; &nbsp; &nbsp; &nbsp; 9.42<br />
2006&nbsp; &nbsp; &nbsp; &nbsp; 7.62<br />
2007&nbsp; &nbsp; &nbsp; &nbsp; 8.29<br />
2008&nbsp; &nbsp; &nbsp; &nbsp; 9.95<br />
2009&nbsp; &nbsp; &nbsp; &nbsp; 3.95<br />
<br />
 <br />
<br />
麻烦在于，2009年中国出现了麻疹发病率大幅下降的趋势，假如此种趋持续的话，中国还真有望自然而然地突破1/10，距离WHO的目标越来越近。真正的麻烦在于，中国麻疹普种持续地无视真正的易感人群（因为儿童受到保护，易感人群已经变化为婴儿与年青成人了），儿童打10针，除了浪费资源，对这些易感人群有什么用？假如中国在2010年麻疹发生率减到1/10万或者0.5/10万，到底是自然趋势呢，还是普种的作用？假如变为5/10万怎么办？方舟子与卫生部会给全国人民赔礼道歉赔偿财务精神损失？<br />
<br />
八、小方舟子疫苗究竟打没打？<br />
<br />
方舟子是没有诚信的，我真想知道小方舟子是否打了第三针：<br />
<br />
&nbsp; &nbsp; 我的小孩虽然此前已接种过两次含麻疹疫苗的麻风腮疫苗，有99％的可能已对麻疹免疫，但是我还是要根据要求让其再接种一次。今天让我们的孩子多挨一针，是为了未来的孩子能够从此不挨针。<br />
<br />
前面已经计算了，这是两项选择：<br />
<br />
&nbsp; &nbsp;1. 小方舟子为未来的孩子不挨针而做了贡献的可能性：0.038-0.245%<br />
&nbsp; &nbsp;2. 小方舟子白挨一针，还把一个需要接种的人的机会夺去的可能性：99.755-99.962%<br />
<br />
方舟子，请您行行好，为三块说不清道不明的青肿，您能出20万悬赏，何必跟身无分文的人争这几块钱的麻疹疫苗呢？您浪费一针，别人就少打一针，谁叫咱们是社会主义初级阶段呢。</div>

							
							
							
							
