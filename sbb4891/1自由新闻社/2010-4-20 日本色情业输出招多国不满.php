　　类似现象其实在西方时有发生。《环球时报》记者的一位德国朋友对记者说，前段时间，她无意中翻开女儿经常看的日本漫画，顿时被里面的画面惊呆了。这些日本漫画书里尽是一些描绘色情、性爱的内容。其中一页是穿着校服的女学生衣服被撕开，还被绳子绑住四肢。这让她感到很无奈，不知怎么教育自己的孩子。毕竟这些漫画都是通过书店出售的，而且不少青少年都在看。德国塔利亚连锁书店的漫画柜台工作人员则对记者说，日本漫画比美国的更含蓄，比如画面、语言虽然隐含色情，但不直接，所以不能被列为儿童不宜或禁止购买的书籍。在英国，家住伦敦西区切尔西的沃特森是一位法律事务顾问，他对记者说：“日本的色情产业的确显示出其科技发达的一面，因为无论是上网，看付费电视或是购买电脑游戏光碟，都可以购买到日本色情商品。我很奇怪日本政府为什么不予以打击，因为这个产业一直在壮大，已经影响到欧洲的许多原本对于日本文化感兴趣的年轻人，让他们走入歧途”。沃特森说，他对日本和日本人一直印象不错，但是了解了日本色情产业后，感到这是一个“患病”的民族，需要通过严格立法来打击蓬勃兴起的色情业。<br />
<br />
　　前不久，上万名电玩爱好者、艺术家、学者参加了在美国波士顿举行的“动画波士顿”展览。这个日本文化展览每年都在波士顿举行，在新英格兰地区是最大型的，但它在美国同类型日本文化展览中只能挤进前十名，可见日本流行文化在美国还是很有市场的。参展的知名美日混血作家罗兰德・凯尔茨在他的《日本美国：日本流行文化怎样入侵美国》一书中，对日本电脑游戏行业以及其中的“变态”系列游戏进行了剖析。他认为日本社会是一个想象和现实有严格界限的社会，日本男性工作很忙，他们最有可能接触到的异性，就是在地铁上碰到上学或放学的青春靓丽的女学生，他们的性幻想不难理解。他说，日本性文化在西方人看来也许很怪，但是放在日本特定的环境中，就不那么怪了。&nbsp; &nbsp;<br />
<br />
日本社会对“性”很宽容<br />
<br />
　　在日本有一首非常流行的歌曲，其中有一句歌词很特别―――“丑闻是男人的勋章”。这里的丑闻主要指婚外情，尽管外人对此难以理解，但却体现了日本社会对“性”的宽容。<br />
<br />
　　走在日本街头，就可以感到其色情业的发达。不但在歌舞伎町这样的娱乐街上到处是妖精打架一般的广告，连一般的24小时昼夜小卖部，都会有一个专区放置色情刊物。在日本的书店里，一面可以是精美的学术著作，另一面就是赤裸相见的色情写真集。而日本寂静无声的电车里，除打盹的乘客外，几乎人人都在看书。然而若稍稍注目，就会发现其中不少人手里拿的是色情杂志，日本的色情杂志通常只有几十分钟的生命，上车买，下车就扔到垃圾桶里。现在日本越发进步了，已经经常可以看到有人拿着手机在打色情游戏。在日本，色情行业已经形成一个完整的产业链条。比如日本色情影碟中经常演出令人惊悚的情节，但实际其中参与的演员不乏专家，比如专业的捆绑人员，使用的道具也不乏特制产品，例如低温下燃烧的蜡烛。据参加拍摄的人员形容，日本拍摄色情片有一定仪式。开拍前全体人员要对女演员双手合十表示感谢，而后按照剧情设计亦“毫不手软”。规模化经营后，日本色情片的制作如同流水线，这一点是其他国家的同行望尘莫及的。<br />
<br />
　　在社会上，日本男性还以拥有情人而自豪，虽然不赞成离婚，但是却对婚外情很是赞赏。男性往往很自豪地声称自己有情人，即使开玩笑也爱把自己和公司年轻漂亮的女职员的关系说得暧昧一些。朝日新闻社出版的周刊《AERA》的特集称，日本公司职员中有20％以上的人有婚外情。<br />
<br />
　　一位日本教授对《环球时报》记者说，日本民族的很多习惯都可以从民族历史中找到根源。在日本古代，海洋渔业占有重要地位，日本人经常要在船上活动，长期出海。因此日本人第一不会有多少对性的顾忌，包括对性的羞怯；第二，男人长期局促于船舱，女人守望于岸边的无性生活，自然让古代日本人充满对性的渴望和幻想。在日本人的潜意识中，性因此有了家园的含义，并因此带有文化上的崇高。这一点，与亚洲其他民族有着相当大的不同。<br />
<br />
　　西方搞不懂日本的色情文化<br />
<br />
　　日本文化在普通美国人看来是由寿司、和服、武士、先进电器、漫画和色情片构成的奇特组合，既有精致唯美的一面，又有暴力怪异的一面。美国人承认日本人有想象力和创造力，日本漫画在美国青少年中非常流行，他们觉得日本文化很酷。但是，不少日本文化中的东西特别是日本性文化，美国人难以理解。美国人想不通为什么日本男人都喜欢青涩的穿制服的未成年少女，喜欢她们受折磨时痛苦的表情？为什么日本人喜欢搞些很变态的色情片和漫画、游戏？通过这些漫画和游戏，美国人认为日本是一个父系文化为主的社会，男性掌握权力，女性只能顺从，这跟男女平等的西方社会截然不同。不少美国人很困惑，日本社会是真的这样，还是只是文学创作中充斥变态的性欲？<br />
<br />
　　对此，德国人的看法相对温和。根据最近几年的国家形象调查，日本和日本人一直在德国人的印象中排在前三名。很多德国人认为，日本是“礼仪之邦”、科技发达，富裕、环保等。日本的色情业虽然展示了日本的“消极一面”，但其负面影响远远不及日本人捕杀鲸来得大，后者才是日本国际形象的致命伤。甚至有不少德国人认为，色情业是日本人性的一面，而且有日本特色。▲（●<br />
