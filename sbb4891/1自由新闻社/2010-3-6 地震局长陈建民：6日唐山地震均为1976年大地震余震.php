地震局局长:6日唐山地震均为1976年大地震余震<br />
2010-03-06 21:37:00　来源: 新华网　跟贴 331 条 手机看新闻<br />
<br />
核心提示： 全国政协委员、中国地震局局长陈建民6说,6日上午发生在唐山地区的两次地震,均为唐山大地震系列衰减活动中的正常余震活动,不会再有强震。<br />
<br />
新华网北京3月6日电 全国政协委员、中国地震局局长陈建民6日下午接受新华社记者采访时说,6日上午10时49分许、11时许发生在唐山地区的两次地震,均为唐山大地震系列衰减活动中的正常余震活动,不会再有强震。<br />
<br />
“1976年发生的7.8级唐山大地震之后,会不断有余震。刚震完的一年之内,余震发生的频率高,强度也大,但根据规律,余震频率和强度是逐步减弱的。”陈建民表示,从1980年至今,发生了33次4级左右的地震;1990年中期至今,发生了9次4级左右余震,而今天以前的最后一次4级左右余震,发生在2004年1月份。<br />
<br />
他说:“6日发生的两次地震,都是唐山大地震系列衰减活动的一次小的正常的起伏活动。如今距离唐山大地震已有34年时间,再有余震,强度也不会大。公众不必恐慌。”<br />
<br />
据中国地震台网测定,6日10时49分许,河北省唐山市滦县、唐山市市辖区交界发生3.1级地震,震源深度10千米。11分钟后,11时许,该地再次发生4.2级地震,震中位于北纬39.7°,东经118.5°,震源深度为15千米。<br />
<br />
新闻回顾：河北唐山连发两次地震 北川发生3.7级地震<br />
<br />
摘要：3月6日上午11时，河北省唐山市滦县、唐山市市辖区交界发生Ms4.2级地震，北京东部有震感，目前尚无人员伤亡和财产损失报告。据此前报道，3月5日凌晨1点52分，四川北川境内发生一次3.7级地震，两条通讯干线中断。<br />
