面对芸芸众生，ZF是否应该有所反思？ <br />
<br />
为什么此次日本宫城7点2级大地震死亡人数这么少，原来在地震前两天的6月12日宫城全县在县知事村井嘉浩领导下进行了防震大演习。 <br />
<br />
县知事在演习中说，宫城大地震９９％要发生，希望全体县民高度保持警惕。结果两天后的6月14日晨果然发生。<br />
<br />
