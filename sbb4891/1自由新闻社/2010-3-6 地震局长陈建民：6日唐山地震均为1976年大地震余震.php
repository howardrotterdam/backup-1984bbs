<a href="http://mainichi.jp/area/miyagi/news/20080613ddlk04040677000c.html" target="_blank">http://mainichi.jp/area/miyagi/news/20080613ddlk04040677000c.html</a> <br />
<br />
<br />
　　“1976年发生的7.8级唐山大地震之后，会不断有余震。刚震完的一年之内，余震发生的频率高，强度也大，但根据规律，余震频率和强度是逐步减弱的。”陈建民表示，从1980年至今，发生了33次4级左右的地震；1990年中期至今，发生了9次4级左右余震，而今天以前的最后一次4级左右余震，发生在2004年1月份。<br />
<br />
　　他说：“6日发生的两次地震，都是唐山大地震系列衰减活动的一次小的正常的起伏活动。如今距离唐山大地震已有34年时间，再有余震，强度也不会大。公众不必恐慌。”<br />
<br />
　　据中国地震台网测定，6日10时49分许，河北省唐山市滦县、唐山市市辖区交界发生3.1级地震，震源深度10千米。11分钟后，11时许，该地再次发生4.2级地震，震中位于北纬39.7°，东经118.5°，震源深度为15千米。</div>

							
							
							
							
