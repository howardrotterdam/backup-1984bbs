十具木偶肃立在橱窗内，个个脸蒙面巾，或着清代服饰，或着深色长衣，身后摆满了花圈。树影和天空映在橱窗内，营造出一种恐怖气息。昨日，路人举报西大望路禧福汇一底商搞恐怖展览，目前，警方已出面协调解决。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; “恐怖”木偶吓坏小孩<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; “妈呀，这不是僵尸吗？真是大白天撞见鬼了！”昨天下午，西大望路禧福汇小区某底商外，一名路过的女孩被吓得不轻，瞪大眼尖叫起来。只见30余米宽的店铺内，10具木偶肃立在两边的玻璃橱窗，木偶个个脸蒙面巾，头戴老式瓜皮帽或礼貌，有的穿着清代服饰，有的穿着长衣，其身后摆满了2米多高的花圈（如图）。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 记者透过玻璃橱窗看到，店中摆满了花圈、骨灰盒和棺材等殡葬用品，但商铺外并无招牌，大门也拴着铁锁。虽然店内无人看守，但雷人的是，里面摆了一桌麻将，三位“僵尸”打扮的木偶围坐一旁――“嘿，还‘三缺一’呢，够逼真了！看着让人}得慌。”一名路过的大爷说。这时，树影和天空就映在橱窗内，增添了一丝恐怖气息。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 小区业主联名抗议<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 记者来到旁边的烟酒食品店，店员说，6月4日看到隔壁停了一辆货车，有人往铺子里搬殡葬用品。“这两天，路过的都打听怎么回事，我们也纳闷呢。昨天，有小孩路过这，直接就吓哭了。这样展览，我们店的顾客也要吓跑了。”<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 记者采访了小区的部分业主。业主普遍认为，底商如此做法一点儿也不顾业主和路人的感受。“白天还好点，晚上经过尤其吓人。这两天回家后，都有心理阴影了。”一名王女士说。记者找到小区物业，回复称，商铺是一名业主的，已经出租给商户，可能要开殡葬用品店。“他们往铺子里搬货的时候，我们也问过，商家说只是把铺子当仓库用，问有无经营执照，说是还在办理。”但王女士认为，即使有执照，也不能如此摆设，“八宝山的殡葬店那么多，也没这架式的。”物业负责人称，已经有40多位业主提交联名抗议书，但物业没有执法权，只能尽力帮助协调。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 警方介入协调<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 记者试图联系出租方，但物业称不便透露业主信息，并转述说，商家不愿接受采访。昨天，记者致电劲松街道办，工作人员称，已经接到举报并派人调查，由于只是室内展览不存在占道，且尚未营业，他们无权干涉商家的行为。劲松街道派出所方面则表示，已经介入此事，正在和物业协调解决。<br />
