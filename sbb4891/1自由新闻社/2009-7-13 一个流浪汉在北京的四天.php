7月9号第一天<br />
<br />
&nbsp; &nbsp; 下午去破破的杂志社办事，随后一起到牛街吃晚饭，途中电话约了李立，鉴于破破喜欢和名人打交道，他们谈些左左右右的轶闻，可以让我俩关于新疆的激烈争论告一段落。饭后到牛街清真寺，水房名曰“涤虑处”，让李立感觉新鲜，我和他讲解洗小净的程序，发现旁边有个维族人动作很优雅，示意李立看看，瘴饰族人来自何处，他有一张枣红色的面孔，微笑着说：喀什。我继续问一些问题，他汉语很糟糕，开始我听不大懂，但是很快明白了：他现在身无分文，一两天没有吃饭，在街角的小花园里过了好几夜。<br />
<br />
&nbsp; &nbsp; 起身来，才看得出他身材矮小，但很健壮，一副干农活的好把式。我问他叫什么，有什么打算，他回答名叫艾尔肯，因为次日周五是主麻日，会有很多穆斯林来做礼拜，他想在人群中找找维族朋友，介绍工作，至少可以找个住处。我和李立给他一点钱，承诺今晚帮他解决住宿问题。先是问寺里的乡老，能不能在长廊下凑合凑合，他们没听完就连连摆手，态度坚决地说不可能。我颇为腹诽，清真寺本来就行使社区服务的功能，收留鳏寡孤独、施予救济，是否穆斯林都应一视同仁，这里作为中国最有名的清真寺，反倒不如乡下。但也不与其理论。考虑到附近价格不菲，住旅馆的话以后几天的饭钱就没着落了，于是决定让艾尔肯到我家住。<br />
<br />
&nbsp; &nbsp; 艾尔肯说还有一个巴郎子（年轻小伙）在附近，我们走向街角的花园，他进去不大会儿从夜色中带出来一个年轻人，名叫阿里江，和艾尔肯一样，这么热的天气还穿着长袖，甚至还套着一件牛仔服，看来真是饿了几天，一点火力都没有了。破破要请两人吃饭，我们边走边聊着。说实话，在这种时刻带两个陌生的维族人回家，我心里詹惶な担破破也有点替我担心，他问了很多问题，弄清楚了个大概：艾尔肯去沈阳一个同乡开的餐馆里打工，答应月薪八百，但几个月一共只陆续支付了四百块，老板打牌赌博把钱输光了，他要不到钱，才离开沈阳到了北京。阿里江是在贵阳打工，不仅没要到钱，还被打了一顿赶将出来。两个流浪汉在小花园相遇，幸亏阿里江身上还有五块钱，买一个馕二人凑合两天。阿里江撩起衣服给我们看背上的伤，这小伙子长得很精神，一头金黄色的卷发，身材瘦高面孔白净，一看就是小帅哥。艾尔肯须眉皆黑，长得不如阿里江有光彩，但是面目和善，很耐看。路过一个灯红酒绿的店面，门口坐着几个男人，有个露着大腿的女人冲向其中一个，环着他的脖子转了一圈，坐到另外一个的腿上。艾尔肯目不斜视走过，阿里江频频回头看。走过一段，艾尔肯跟我说，这边有没有网吧？我大为惊诧：你还会上网呐？再一想，哦，是阿里江的主意吧？阿里江赶紧说，我们去网吧住一夜就行了，不用去打扰你了。我说先吃饭，别的再说。<br />
<br />
&nbsp; &nbsp; 我家门口有个小小的新疆餐厅，实际上是撒拉族开的，问了问，不需要人手。我去给他们买牙刷，回来看他们两碗拉面就着一个烤馕，吃得满头是汗，问能吃饱么？阿里江说饱了饱了，艾尔肯说，饿太狠了不能吃多。破破付了钱，嘱咐我多照顾两个老乡――他刚才还说维族人都是匪徒――然后上车回家了。<br />
<br />
&nbsp; &nbsp; 我住的老房子四壁不隔音，邻居们从来没显示过宽宏大量，所以一再叮嘱轻声一点，后来发现他俩都很规矩，纯属多虑。倒是阿里江的脚臭让人难以忍受，这小子住在清真寺边上也不礼拜，臭味都来自他，勒令其马上洗澡。艾尔肯背的破包里也都是换下的衣服，两个人洗了十几件。艾尔肯是虔诚的穆斯林，十点钟做完礼拜就躺下睡了。阿里江在我身后看电脑，我打定主意不给他玩儿，让他赶紧睡觉。<br />
<br />
&nbsp; &nbsp; 在MSN上和著名DJ优优谈到此事，他异常夸张地说：真的？！他们在哪儿？我说就在我身后睡觉，他叫起来，说明天一定要来看看他们，还说我太伟大了。我劝他不要随便戴高帽，原因很简单，我是单身汉，号称自由职业，又是租来的房子，有正常家庭生活的人很难象我这样随便。以前我收留过藏族人，就在昨天还收留过一个汉族人，都是这个原因。但是有一天晚上我回家，小区门口有个带锁的铁门，通常不关，一个年轻女性在前面走，进门以后突然转身把门撞上，我被拦在外头，赶紧跟她讲没带铁门的钥匙，住几单元几号，她说你自己想办法吧，转身走了。我理解她的行为，但也忍不住想，汉族社会本身的问题不解决，民众普遍的精神格局是不适合领导一个多民族国家的。房龙在名著《宽容》中说：不宽容的本质是恐惧。<br />
<br />
 7月10号第二天<br />
<br />
&nbsp; &nbsp; 艾尔肯四点多就起来礼拜，阿里江也早早起来，两人吃过昨晚捎回来的馕，静悄悄等到我醒来。他们想尽早去牛街，看看有没有什么维族朋友。昨天问起乌鲁木齐的情况，阿里江在捡到的报纸上看过一些，艾尔肯则一无所知。现在他们都知道事情比较严重，有些紧迫感了。<br />
&nbsp; &nbsp;路上我继续询问其家乡的情况，维族人没有学会有所保留的交往方式，问到收入，他们一律先说高限，再说低限，往往一开始让人觉得还可以。艾尔肯是喀什巴楚县人，开始说3000，后来才问清楚，原来是18亩地一共年收入1500到3000。而且，他父母健在，还有兄弟六人、姐妹三人，艾尔肯排行老七也已经28岁，早已到了婚育年龄，但所有兄弟一共只有两个孩子。根据我看过的资料，建设兵团不仅人均拥有更多土地，还可以开垦新地，兵团职工除了种地收入还有养老保险等等，和城市职工待遇一样，而维族农村从83年起就不再新增土地，只能继承，全部收入来自有限的土地，多一口人就增加一份口粮负担。这样的政策显然严重制约维族人口发展，在艾尔肯家的情况中得到了证实。但没想到的是，巴楚县至今没有贯彻减免农业税，包括水费、管理费、地租等等名目一亩地要收惊人的700多块钱。小麦亩产最高一千斤，只能卖400元左右，他们留四五亩地当作口粮，棉花作为经济作物产出高一些，但统购统销，由政府定价。可以想见艾尔肯家里的赤贫情况。阿里江家所在的轮台县已经贯彻了减免农业税，每亩只缴纳一百多元的水费，有果园、棉花，人均收入应该和内地农村差不多。不过最大的问题是基本上没有打工收入，问他们有什么手艺，都是做大盘、拉条子、烤羊肉，维族餐馆才会有多少呢。我大学的维族同学，即便是名校硕士学历也有回家卖羊肉串的，就业歧视不仅针对低学龄。赤贫已经成为显著问题，所以当地政府开始联系对外务工，成为政治任务以后又产生新的问题，阿里江说，务工需求最大的是纱厂女工，由于女性不愿意出远门，如果哪一家被指定派人外出务工而不愿意去，会被勒令收回土地。问他们怎么看待汉人，阿里江说打架的话只抓维族不抓汉族。艾尔肯沉默了一会儿，双手做了一个抓握的动作，说：我们就像小树，他们拧我们，不让我们长大。<br />
&nbsp; &nbsp; 果然在路上就碰到一个阿里江在乌鲁木齐认识的人，那人起初有点冷淡地看看我，他们聊了几句，对我热情了起来，后来我知道他叫马佳，父亲是回族母亲是维族。往前走几步，原来马佳还带着妻子以及一岁多的孩子。那小孩儿黑黑的，长得很漂亮，我一边逗他玩儿，一边问马佳的情况。他到河北投奔妻舅，从伊犁刚到乌鲁木齐就遇上骚乱，不敢久留，昨天到的北京，今天早晨旅馆检查，不许他们住了，现在正发愁。马佳希望把行李寄存到我家，他们不知道从哪里拎出来四个包，有个似乎是马佳弟弟的人跟我一起打车回家，还交待我要把包里洗过的衣服晾一晾。<br />
&nbsp; &nbsp; 下午，我思忖聚礼差不多结束的时候，又到牛街去找他们。清真寺附近拉起了警戒线，有三四辆警车在巡逻，看到卖馕的小贩就用扩音器吆喝离开。人已经散得差不多，没找到艾尔肯和阿里江。我想他们获得帮助的可能性不大，碰到的大多数是象马佳这样需要帮助的人。果然，回来的路上遇到艾尔肯，他说阿里江跟朋友借了50块钱，到天津投奔舅舅了。他俩大概不会说舅舅这个词儿，阿里江说过他妈妈的兄弟在天津，但好像只是个小店，不需要那么多人手。艾尔肯还没有吃饭，我拉他先去解决肚子问题，他突然眼圈红了，说，把你的钱都花光了。昨天我第一次和他说话，他说“没有钱了”的时候眼睛望着另一边的窗口，接过钱的时候也不看我。也许因为他有一双明亮的眼睛和超乎寻常的睫毛，我根本没有看出他落魄到连感谢也无力表达的程度。现在大概好一点了。<br />
&nbsp; &nbsp; 既然打工无望，看来唯一的办法就是回家。昨晚优优自告奋勇要给两人买车票，但有个央视的朋友告诉我，他采访民政局时得到一个信息：收容救助站可以领取免费的返乡车票，只要提供身份证，查明确属贫困、没有犯罪记录即可。我谢过优优的好意，决定带他们试一试，了解这个程序，以后出门也方便些。打电话到宣武救助站，接电话的人说：从来没有免费领车票这一说，你想得太好了。看来是个幽默。在附近的售票处打听，去往乌鲁木齐的车一律没有票。艾尔肯说在西安有个朋友可以投奔，但几天内到西安的都只有站票，于是又给优优打电话，他可以想办法。<br />
&nbsp; &nbsp; 晚上优优来，我又叫上了喜欢见名人的破破。他们都很喜欢艾尔肯，跟他聊了很久。也许听到的不幸太多，优优转而问他，你一生最幸福的时刻是什么时候？艾尔肯的回答让我们哄堂大笑：结婚的那一天。其实他的婚姻只维系了半年多，他的妻子是“爸爸的弟弟的（女儿）”，问及离婚的原因，他嗫嚅着说：穷的事情嘛，没法说。优优转而聊自己的专业，艾尔肯好像会弹冬不拉，可惜没有条件表演。<br />
&nbsp; &nbsp; 破破按时回家，临走前给艾尔肯一些钱，从昨天开始，破破给我的好感已经颠扑不破了。我和优优通过网络相识已久也有很多共同的朋友，但还是第一次见面，这次两个夜猫子有机会秉烛夜谈。艾尔肯做礼拜的时候，优优在旁边看得出神，感叹诵经真好听。他说生活没有给艾尔肯任何希望，但信仰给他希望。那个詹或诚的阿里江，却对宗教自由不能贯彻颇有微词，他抱怨当地成年人之间如果劝告进入清真寺的话，会被劳教。不久前有一个在吐鲁番任副县长的朋友来北京，谈起自己分管宗教事务却受限于公职人员不许进清真寺的规定，难以开展工作。有人认为新疆的社会矛盾体现为宗教与世俗的张力，其实很多时候是政策张力，不合理的政策即使大部分人已经习惯，迟早也会出问题，象阿里江这样虽然不是虔诚的穆斯林，但如果流浪过程中遇到一些人煽动，难免也会加入地下宗教组织，成为“宗教分裂势力”的一分子。<br />
<br />
7月11号第三天<br />
<br />
&nbsp; &nbsp; 既然决定了去西安，优优那边的车票也已经有着落，我心放下了就可以睡个懒觉。早上醒来的时候，艾尔肯已经把屋子收拾了一下，因为不识字，百无聊赖地在玩哑铃。看我醒来，他说自己弟弟最喜欢这个。我说这玩意儿不能送给你，太沉，另外一个倒是可以，你猜猜那是干什么的？他搬弄了半天也没弄明白，我套上手腕弯了两下，他才看明白是个腕力器，吃吃吃笑得像个小孩儿。<br />
&nbsp; &nbsp; 想到他要走了，我有点惆怅。昨天只顾为他找出路，没仔细问问到西安怎么安排，想来他也不可能有什么安排。帮他收拾行李，主要是衣服，从破烂的背包里挪到我给他的旅行箱。他说这个箱子很好看，我心里嘀咕那是POLO，两千块呢。昨天说起他家里没有电视，我脱口而出我也没有，后来很惭愧，象我和优优这种不看电视的清高，和南疆看不起电视的窘困，是完全不能相提章鄣摹０尔肯说家里没有水管（自来水），喝水到附近的河里取，那条河有十米宽，水很清。想来也是十分艰难，艾尔肯在做礼拜前洗小净，还有帮我清洗厨房的时候，都反复调整水龙头，形成最细的水流才用。<br />
&nbsp; &nbsp; 巴楚县2003年发生过地震，造成一人死亡，这后来成为当地政府拆除唯一现存的见证过丝绸之路的喀什老城来发展房地产的理由。地震中艾尔肯家的房屋倒塌但没有人伤亡，政府补贴到户2000元。南疆的住房由没有煅烧过的生砖所建，盖房子是真正的家务活，艾尔肯估算建造一座普通住宅要打7000块砖，门窗需要购买，共需5000元人民币。结婚要花费8000到15000人民币。我问艾尔肯什么时候能够挣到盖房娶媳妇的钱，他很有豪情地说，打算回家养羊。<br />
&nbsp; &nbsp;&nbsp;&nbsp;艾尔肯给亲友打电话报平安，掏出一个反复弯折字迹模糊的硬纸板，我让他誊抄到一个小本子上，他小学没毕业，维语写得也很差，我看出这是阿拉伯字母，告诉他我会念但不知道什么意思，他说爸爸妈妈学的是另外一种维语，似乎还有点骄傲。六二年政府改造维文，用拼音字母代替传统维文字母，书写顺序从右向左改成从左向右，此前识字的人就变成了文盲，艾尔肯爸妈那时候学的是新维文，二十年后又下令改回老维文，他爸妈这一代又成了文盲，再加上生活所困，他们既难以供养又不能指导下一代读书，继续制造艾尔肯这样的文盲，不知会到哪一代才结束。<br />
&nbsp; &nbsp; 下午，有一个兰州来的王老兄约了吃饭，也很想见见艾尔肯。出门之前我想起今天是信用卡还款日，上网操作了几下，艾尔肯在旁边看得津津有味，这些现代的玩意儿他显然不明就里。我突然想起黄仁宇在《郝逊河畔谈中国历史》中提到，美国人觉得自己在全球都是公平交易，不理解为什么被敌视，黄仁宇认为以美国对发达资本主义的熟悉，向穷国释放最嗜利的商人，无疑是巨人和侏儒的决斗，奖品则是该国的自然资源。王老兄显然不同于新疆土著，生意做得很发达。他欣赏艾尔肯的虔诚信仰，鼓励他还要相信党的政策，多学习知识改变命运，也很慷慨地资助。艾尔肯的眼圈又红了，无声无息。他来北京的时候身上只有150块钱，从火车站步行到天安门，然后打车到牛街花了30多，被多宰一倍，剩下的吃饭几天就花完了，虽然一贯穷困，但这几天在居大不易的京城，应该是最感到钱的金贵。王老兄想在北京开家规模很大的餐厅，正在找地方，我突然想起来，有个朋友曾经在一家很有口碑的新疆餐厅工作，没准可以试试，打电话过去说了一下，让艾尔肯自己跟对方交流，虽然听不懂维语，但看得出他面有喜色，拿过电话，果然对方说正在招人，过来试试吧！<br />
&nbsp; &nbsp; 我们按捺不住心情激动，打车直奔该地。餐厅的经理是个汉语说得非常棒的维族小伙，我知道他还会英语。我那位朋友以前也是这里的经理，他从新疆刚到北京的时候只会说维语和不太熟练的汉语，呆过几年之后，不仅普通话已经很熟练，还学会了英语和阿拉伯语，现在在一家使馆工作。现任经理非常就事论事，面无表情，甚至有点怀疑地打量我们瘴柿艘恍┪侍猓艾尔肯回答的时候表现腼腆，侧着脸，下巴搭在锁骨上。我帮腔说，他是个很勤快的小伙子，经理不置可否地说我们这里的都很勤快，然后跟老板打了个电话，结果令人喜出望外：这人今天就可以留下了，底薪一千，岗位待定，要求提供身份证，到派出所报道一下，服从严格管理集体住宿。这时候餐厅经理才露出微笑，对我和王老兄说：谢谢你们。<br />
&nbsp; &nbsp; 相对于收入，“严格管理集体住宿”对一个完全外在于潜规则社会的人才是真正的福音。对一个老实巴交一心想卖力气挣钱的农民，还有比这更好的结果吗？艾尔肯高兴得咧着嘴一直笑，我和他拥抱告别，说很快把行李拿来，随王老兄喜气洋洋地走了。打电话给几个关心此事的朋友并嘱优优把票退掉，都别提多高兴。<br />
&nbsp; &nbsp;&nbsp;&nbsp;最后一件事，马佳的行李还在我家，今早他和我联系，说昨晚睡了一夜草地，大人还能将就，小孩子受不了。我的蜗居虽然住得下一家人，但就容不下我了。思来想去，打定主意到朋友家借宿，马佳已经买到13号去石家庄的车票，最多在我这儿住两晚。晚上接到电话，下楼去远远看见马佳和他弟弟，才一招手，他们就飞奔过来。维族人真是性格奔放，都成年人了，在矜持的城市人中间这么跑，以为人家不知道你是骚乱地区来的吗？所幸四下无人，我带他们上楼的时候又听到一个圆满的消息，他们联系好了住在郊区的一个朋友家。到了睡个好觉的时候了。<br />
<br />
7月12号第四天<br />
<br />
&nbsp; &nbsp; 昨夜为了安眠关掉手机，下午在我开始写“流浪汉的三天”时才收到一条短信，使本文变成了“流浪汉的四天”。是王老兄的短信转告我：艾尔肯不见了！<br />
&nbsp; &nbsp; 打电话到餐厅，完全是不耐烦的情绪。艾尔肯早上六点去上班，店里值班的人说九点才允许进入，他离开后就再没消息。我以自己对艾尔肯的三天观察，肯定他不会有什么不良企图，至少不是一个恐怖分子。餐厅经理推心置腹地说：你和我都不知道他心里想什么，你看得到他的眼睛，看不到他的心。看来最大的可能是迷路了，而且给我打电话的时候正好关机，果真如此倒是丢不了，但是要先去安抚餐厅那边。为方便找人，骑自行车赶过去，见到经理后尽力解释，承诺一定把人带回来让他们问清楚，真有什么问题我也不会麻烦他们。得到首肯，开始在附近转悠。有个朋友小枫对此地非常熟悉，她碰巧就在附近，指点我去问这个大院子的保安，保安又指点我们去看中心监控，这是个好办法。向警卫详细说明了情况，但是监控录像属于保密范畴，专业人士才能看，我们必须等待。<br />
&nbsp; &nbsp; 和小枫一起来的还有她的同乡和我的好友纳赛尔，一位巴勒斯坦留学生，我正在雨地儿里祥林嫂般的向他们描述这个事件，手机响起来，一听是艾尔肯，劈头盖脑地骂了他一通，他解释说，早上给家里打电话，爸妈年迈干不了农活，想让他回家。我不听，先过来再说，不出所料他在牛街附近的商店打电话，我没好气地命令他把电话给旁边的人，他不情愿也没办法，这几天我已经对他建立了权威。我请商店老板帮这个人打一辆出租车，崭嫠咚净地点。挂了电话稍微喘了口气，又向几位关心的朋友报平安，然后大家开始研究怎么劝服他的问题，甚至要利用纳赛尔的相貌和身份来吓唬他。等了半响，觉得不太踏实，又给刚才接到的号码去了一个电话，商店老板说哪儿有打车，那个人转身就走了。<br />
&nbsp; &nbsp;&nbsp;&nbsp;小枫、纳赛尔建议我再去牛街找艾尔肯，他们在此等待，如果艾尔肯到这里就给我打电话。事已至此，看来多半是丢不了。我和小枫、纳赛尔久违，干脆小坐一会儿再说。闲谈中得知，原来小枫和这个餐厅老板是朋友，这个后台可比我的间接关系硬多了。我问纳赛尔有没有看过中国四大名著之一的《西游记》，他似乎只记得一只猴子，我跟他讲，这里头博大精深，猴子那么厉害，为什么许多妖魔鬼怪都打来打去打不死，白骨精却一棍子打死了？因为那些妖怪起先都是天宫的宠物，白骨精却没有后台。所以，我看到餐厅经理走过来，立刻拉着小枫说，瞧，她是我的朋友。<br />
&nbsp; &nbsp; 又骑行十几公里返回，牛街清真寺里没看到人，附近转了转，还是在小花园找到了艾尔肯。他躺在一条长凳上睡得正香。我拍他起来，却不敢出口骂了，和颜悦色聊了聊，晓之以理晓之以利，在这儿打工多好啊，是吧。他又说了一些新情况，原来家里发生了旱灾，那条河干了，饮用水都没有，田里需要更多水，打水要走很远，家里劳力不够。我说那就把打工收入寄回去一部分，雇人帮忙打水。他似乎从来都没有考虑过这个方案，说服工作比我想象得简单许多。我打电话到他家，企图帮他说服家里人，没想到艾尔肯这种汉语水平居然是他们家最好的，那边换了两个人听不懂就给挂了。再打过去让他自己说，我有些不放心，不过还是说服了。听口气，他根本就没向家里说清楚，现在不是在沈阳，是在伟大的法治首都北京。最后家里交待，要努力工作。就这种糊涂蛋，我也就不谴责他不会办事了，“办事”本来就不是他的生活内容，只要求他，以后无论怎么想，先要给我打电话，然后指着MSN上优优的头像说，这哥们儿今天急得去昌平翻砂场找你去了。艾尔肯虽然不知道翻砂场是什么意思的，但是这一招似乎有效，他以后会多个心眼吧。但愿。<br />
&nbsp; &nbsp; 早上给我打电话不通以后，艾尔肯就步行了十几公里才走到牛街，累的够呛，做完礼拜就沉沉睡去了。刚才他又起来做晨礼，问我说，大哥，你不困吗？我指着电脑：把你的事儿写写，在网上发表。看他不明白网是什么意思，我又说，你上报纸上电视了。他咧开嘴呵呵笑了，笑了又说，有什么用处？我不知道。大家以后多去正宗的新疆餐厅吃吃饭，应该容易的吧。</div>

							
							
							
							
