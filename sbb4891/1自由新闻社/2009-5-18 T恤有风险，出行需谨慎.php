&nbsp; &nbsp; 2009年5月12日下午5点多，我在广州市岗顶地铁站靠近C出口的地铁闸口外面等我女朋友。因为她带了很多书，所以我来接站。等了有10多分钟，左等右等还是&shy;不来。电话联系知道她已经快到了。 <br />
