&nbsp; &nbsp; 这时，一个警察（警号是018464）来了，另一个保安（2409号）来了，地铁工作人员数人来了，其中地铁一人手持相机对我拍照。围观的人越来越多，人们以新&shy;奇和不解的目光注视着我的衣服和眼前突发的事情。在警、保、铁三方如临大敌、铜墙铁壁的包围中，在围观人群不明就里的注视中，我犹如汪洋中的一条独木舟。 <br />
