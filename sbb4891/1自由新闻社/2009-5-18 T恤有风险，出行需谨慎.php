													<h2>我在岗顶因穿了一件文化衫被限制人身自由3小时</h2>
						
						
																											<div id="postmessage_95957" class="t_msgfont">（转载）<br />
<br />
一句话点评：二狗子往往比主子更可恶。 <br />
<br />
<br />
<br />
只因穿了一件文化衫，却招来了被非法限制人身自由3小时的额外之灾。<br />
<br />
2009年5月12日下午5点多，我在广州市岗顶地铁站靠近C出口的地铁闸口外面等我女朋友。因为她带了很多书，所以我来接站。等了有10多分钟，左等右等还是不来。电话联系知道她已经快到了。 <br />
<br />
这时一个保安（胸牌号PT1776）径直向我走来，一双警觉的眼睛上下打量着我的衣服，一脸凶悍地问道：&quot;你是干什么的？在这里干嘛？&quot;我说我在这里等我女朋友。这个保安瞪着凶神恶煞的眼睛，指着我的T恤衫说：&quot;你的衣服不对，这是什么意思？不能在我们这里穿！&quot; <br />
<br />
我自制的T恤衫前面印了&quot;一党独裁，遍地是灾。――《新华日报》&quot;几个字，后面印了&quot;共产党反对国民党的一党专政，绝不是要建立自己的一党专政。----刘少奇&quot;这样一句话。后面的字较小，现在被保安发现&quot;不对&quot;、引起阶级斗争警觉的是我恤衫的前面。 <br />
<br />
我反问道：&quot;我的衣服怎么不对了？我穿什么衣服关你什么事？我在这里等人接人碍着谁了？&quot; <br />
<br />
这时，一个警察（警号是018464）来了，另一个保安（2409号）来了，地铁工作人员数人来了，其中地铁一人手持相机对我拍照。围观的人越来越多，人们以新奇和不解的目光注视着我的衣服和眼前突发的事情。在警、保、铁三方如临大敌、铜墙铁壁的包围中，在围观人群不明就里的注视中，我犹如汪洋中的一条独木舟。 <br />
<br />
&quot;你上面这句话（指&quot;一党独裁，遍地是灾&quot;）不对，要跟我们去调查。&quot;保安满脸阶级斗争。<br />
<br />
女友短信及多个电话告知我已经到站。为了不耽误我的正事，我边环顾地铁出口寻找女友，边指着胸前的文字对保安解释：这是共产党自己说过的话，是新华日报的社论，如果你读过书的话，应该知道抗战时期有个《新华日报》，你可以自己去查，并且告知他我是律师，请你不要妨碍我的人身自由。1776号保安暴跳如雷，露出了十足地痞的面目，当着警察以及众多围观人群破口大骂：&quot;你个傻逼，管你是什么师，我读书比你多得多。&quot;并且当众对我讪笑道：&quot;就你这个样子也有女朋友？&quot;面对这个满嘴喷粪的人渣，我怒不可遏，一阵恶心。<br />
<br />
警察态度虽然没有保安恶劣，但是显然把我当嫌疑人看待，这让我很不舒服。我给他解释：这句话是1946年3月30日中共中央机关报《新华日报》的社论，并跟他说，我女朋友已经出站了，现在她找不见我，我要去接她。警察不许，并要对我&quot;进行传唤&quot;。我指出这是对我人身自由的非法限制，我接不到女朋友，她会很着急。这时警察人数已增为几人，有的强硬，有的和缓。他们和保安将我推拽到一处空地上，然后地铁人员迅速地在我周围竖起了屏风帷帐，将我圈在墙边，以隔阻围观人群探寻的目光和我恤衫文字的对接。 <br />
<br />
这阵仗有如一级战备，我头脑中迅速闪过了十几岁时看过的日本电影《追捕》中的一个镜头：杜丘带着真由美骑马逃跑，前有围堵，后有追兵，前面围堵杜丘的那一面面盾牌树成了一道长墙，背景音乐是以&quot;啦呀啦......&quot;为歌词的激动了整整一代人的粗犷豪迈歌声......《追捕》中堵截杜丘的那一面面盾牌和今天围堵我的地铁帷帐，何其相似乃尔！这种相似性让我哭笑不得。 <br />
<br />
我跟警察指出这是对我人身自由的非法限制，希望放我走，不要再阻止我接人，但是警察不允。警察和保安七手八脚将我拖入一个密室里面&quot;要调查&quot;。拖拽中，我的左臂肘部受到擦伤。 <br />
<br />
到了密室里面，1776号保安更加肆无忌惮地逞凶，当着多个警察和保安的面对我破口辱骂&quot;操你妈！&quot;并且撸胳膊挽袖子冲过来要打我，这时被警察拉住。后来警察在没有征得我同意的情况下，一众人马就对我前前后后地拍照，我很坦然地主动摆好&quot;pose&quot;，配合他们拍照，大概照了有十来张。 <br />
<br />
警察问我衣服上这句话的出处。我向他们作了详细说明，并且提出愿意帮助他们在网上搜索这句话，警察说这里上不了网。但是我相信他们通过一定的渠道验证了这句话的存在。后来见到的警察有039780、039722、018552，其中018552号警官是地铁派出所张姓副所长（警号、保安号等信息是我即时用手机记录下来的，当时连一支笔都没有带）。 <br />
<br />
我要求警方向我出具对我进行留置或者其他类别强制措施的法律手续，但是警方称没有。我要求做笔录，先是被拒绝，后来在变换了一个拘禁地点后，才应允我。我于晚上19时自书了一份《我被非法拘禁的经过》交给警方，要求对方&quot;赔礼道歉，赔偿损失。&quot;我告诉警方：这是非法拘禁，我作为律师日后将会告你们，并且会把这件荒唐的事情传上网。 <br />
<br />
一位警察态度明显和缓，要和我&quot;交流思想&quot;，称：&quot;抗战的时候，国民党很独裁，那个时候共产党反对国民党的一党独裁，所以发表了那篇遍地是灾的社论。但是这句话放到现在，就很敏感......&quot;云云。 <br />
<br />
我回应道：我是一个律师，做的是法律工作，我判断是非的第一尺度是法律，符合法律规定的事情没有人能够阻止我去做，违反法律的事情我压根不会去做。&quot;依法治国&quot;不是已经入宪了吗，是依法治国，不是依&quot;敏感&quot;治国。一警员经过较长时间的伏案工作，终于敲出了一份针对我的&quot;讯问笔录&quot;。我粗略地看了一下：上面关于涉嫌违法的事由是&quot;制造谣言和扰乱社会秩序&quot;。我讥笑着对张副所长说：&quot;你不觉得这太侮辱公众常识了吗？如果把它放到网上去，你不怕这个东西会气死躲猫猫？&quot;张副所长无言。鉴于笔录中多处与事实不符，并且警方不同意我复印该笔录的要求，所以我顺理成章地拒绝在那份笔录上签字。 <br />
<br />
或许是警方已经查证了那句&quot;敏感&quot;话的出处，或许是与我对程序的熟知和严谨苛责有关，或许是警方觉得凭一件文化衫就拉一个人太过意不去（特别是那个喷粪保安的行为太过出格），或许是受我要告他们和上网的声言所影响，最后警方人员摆出了要和解的姿态，问我有什么要求。我说我的要求已经写在纸上，就是&quot;赔礼道歉并且赔偿损失&quot;。警方说：如果你愿意，可以让那个保安来给你赔礼道歉。我说：除了赔礼道歉，还要赔偿损失。 <br />
<br />
晚上8点10分，我离开岗顶地铁派出所。从开始在地铁闸口被盘查，直到被放走，前后历时近3小时。 <br />
<br />
在和女友回家的路上，我满脸疲惫，感慨万千。今天的环球已然大不同于抗战时期，星星不再是那颗星星，月亮也不再是那个月亮。我不知道，在人类近三分之二的国家已经采用民主制度的今天，因为将某一政党昔日曾经揭示了客观规律的一句历史名言印在恤衫上，而受到后世同一政党（政府）如临大敌之生猛对待的，在当今世界扳着指头数，究竟还能扳倒几个指头？（广东经国律师事务所律师刘士辉2009年5月15日于广州）<br />
