																											<div id="postmessage_388007" class="t_msgfont">世界贸易组织(WTO)周一驳回了北京方面就WTO在8月份一项裁决提出的上诉，从而给中国在WTO遭受的最大一场挫败划上了句号。WTO的这项裁决反对中国对好莱坞影片及其他西方媒体内容的在华分销采取限制措施。<br />
<br />
如果中国政府的遵照这一终审裁决，则有可能给美国及其他国家的企业带来福音。虽然中国市场庞大，但对于美国的电影制片企业来说，中国仍是盈利能力最差的市场之一。中国每年仅允许公映20部外国电影，通过互联网销售音乐也为非法，苹果公司(Apple Inc.)及其iTunes音乐下载业务因此在华面临障碍。<br />
<br />
美国贸易代表柯克(Ron Kirk)说，美国企业及工人在这些领域具有领先优势，他们理应依照WTO的既定规则全面享有竞争机会；我们希望中国能迅速响应这些裁决，并拿出具体执行措施。<br />
<br />
中国现在有一年时间来履行WTO的上述裁决。如果中国拒不执行，美国则有权比照音乐、电影、甚至电游厂家的收入损失规模来采取贸易制裁措施，具体金额可能高达数十亿美元。<br />
<br />
截至发稿时，记者未能联系到中国政府官员对此置评。<br />
<br />
WTO官员驳回了中方的上诉理由，即中国是出于维护“公共道德”的考虑而施行了一系列限制法规，如要求外国公司通过一家名为中国电影集团公司(China Film Group Corp.)的国企分销媒体产品。<br />
<br />
此前人们本就不看好中国的上诉理由能令WTO的仲裁人改变主意。维护公德的主张仅在WTO历史上提起过一次，那是美国在2005年据此为禁止互联网赌博进行辩护，但最终以失败告终。霍金&#8226;豪森(Hogan &amp; Hartson LLP.)律师事务所驻布鲁塞尔的贸易律师米祖林(Nikolay Mizulin)说，WTO周一的裁决几乎在每个人意料之中。<br />
<br />
中国为维护其贸易政策在过去一年中疲于应付。在2001年加入WTO后，中国现在已经成为出口额仅次于德国的第二大出口国。今年中国和西方贸易伙伴在包括知识产权、品牌名、禽类贸易、轮胎关税、反倾销规定以及原材料出口配额等诸多领域发生了摩擦。WTO发表的正式报告说，中国还一直是保护主义措施针对的最大目标。WTO官员说，中国已经加强了在日内瓦的法律队伍，并计划在WTO内部提起更多诉讼。<br />
<br />
WTO周一还表示将就欧盟、墨西哥和美国针对中国限制原料出口的申诉展开调查。这些国家称，中国利用出口配额及关税手段使得国内企业能以较国际市场便宜的价格获得焦炭、矾土和镁等原材料产品，称此举违反了WTO规定。<br />
<br />
中国是在两年前大宗商品降格居高不下之时推行的这些措施，旨在保证国内市场的充足供应，平抑价格。中国制造商现在生产的产品复杂程度越来越高，诸如手机和电脑等，这使得它们愈发依赖低成本的原材料。<br />
