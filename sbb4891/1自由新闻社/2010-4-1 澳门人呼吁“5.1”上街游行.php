																											<div id="postmessage_593871" class="t_msgfont">Apple Daily苹果日报网上版－－2010-05-02<br />
讽曾荫权张宇人官商勾结<br />
6300打工仔上街　声讨无良吸血鬼<br />
<br />
<img src="http://s1.postimage.org/NVpY0-5f35d1fb8e4107a922b29c4cd7ac8215.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
3500人参加由职工盟发起的劳动节游行，争取最低工资不少于时薪33元。李家皓摄<br />
<br />
【本报讯】当饮食界立法会议员张宇人「乱q」廿四为最低工资开价；当特首曾荫权面对日渐恶化的贫富悬殊仍坐视不理，打工仔惟有靠自己力抗无良雇主、无能官员。6300名市民昨日在烈日当空下上街，参与不同团体发起的5.1劳动节大游行，力撑最低工资时薪不少于时薪33元。有游行人士更穿上露出假臀部I烟囱，怒斥张宇人凉薄，是「屎忽鬼」。&nbsp; &nbsp; &nbsp; &nbsp; <br />
记者：卢文烈、袁慧妍、翁煜雄<br />
<br />
昨日5.1劳动节，全日最少有五个团体发起游行，其中职工盟主办的反贫富悬殊大游行最具规模，主办单位报称有3500人参加，警方指有1900人。职工盟多个属会包括国泰空姐及机师工会等都有派员出席，记者协会亦首次参加游行，争取改善记者待遇。游行也吸引不少80后青年参加，他们争取劳工权益之余，亦希望尽快落实双普选，踢走漠视工人权益的功能组别议员。<br />
<br />
「高官离谱　民怨爆煲」<br />
<br />
<img src="http://s1.postimage.org/NVxrr-5f35d1fb8e4107a922b29c4cd7ac8215.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
有小区工作者戴上张宇人肖像面具，穿上露出假臀部的I烟囱，讽刺张宇人是「屎忽鬼」。李家皓摄<br />
<br />
「没有公平，那有和谐；高官离谱，民怨爆煲！」游行人士下午3时先在维园集合。主办单位将张宇人及曾荫权「化身」成官商勾结的吸血鬼，在游行队伍中打头阵，讽刺两人犹如吸血鬼，剥削工人利益。当游行队伍抵达政府总部，职工盟更向游行人士派发纸袋，发起工人谷气行动，游行人士先把满腔怨气吹入纸袋，然后用手拍打纸袋，制造声响，最后再向吸血鬼怒掷烂袋泄愤。<br />
昨也适逢是国际家务工日，多名家务助理化身成「妈姐」，一手把玩假辫子，一手抱着假婴儿娃娃，上街争取合理待遇；有外佣化妆成血流披面，又被「锁链」重重围锁，寓意自己有如奴隶，希望政府将外佣纳入最低工资保障范围之内。<br />
最低工资立法进入关键时刻，游行主要诉求是争取最低工资时薪33元，任清洁工的郭女士，每日工作11小时，月薪5600元，实时薪仅得22元。她说，扣除租金约4000元，每月只得千多元使费，现时只能靠积蓄维持生活。她强调，商界建议的24元最低工资根本不能b口。任点心师傅的罗邓敬虽然月薪万多元，但也力撑其它低薪同业，她希望政府改善打工仔工时过长的问题，「我O职业妇女唔止打一份工，出面一份，返到屋企洗衫煮饭，做阿四一份，管教子女又系另一份。」<br />
<br />
再争每日八小时工作<br />
<br />
<img src="http://s1.postimage.org/NVuXi-5f35d1fb8e4107a922b29c4cd7ac8215.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
穿毕业袍的青年，也上街宣泄不满。<br />
<br />
职工盟秘书长李卓人强调若最低工资订于时薪33元以下，打工仔必定继续上街抗争，而继成功争取最低工资立法后，工会下一波运动是争取立法规管每日8小时工作。他又警告，资方在经济好转应与员工分享成果，否则工会会采取工业行动，以罢工争取合理待遇。<br />
另外，多个团体包括街工、社民连、工联会等，昨日早上也分别发起游行到政府总部抗议，要求政府订立最低工资不少于33元，以及规定工时上限每周不逾40小时。其中工联会报称有2500人参加游行，连同其它团体则有2800人，警方则指工联会游行约有1200人参加。<br />
约10名小区工作者亦举办游行，发言人冼昭行不满早前张宇人提议最低工资为20元，后来又改口指24元就足够，指张宇人「非常勇敢捍卫大财团利益」，故向戴上张宇人面具、穿上露出假臀部I烟囱的同行示威者，颁发「大屎忽勋章」，怒斥张宇人凉薄，是「屎忽鬼」。<br />
<br />
个案一：短约员工　无缘增值<br />
<br />
政府拨款给志愿机构开设活动工作员3年短期合约职位，供中三及中五毕业生申请，机构可每年续约。22岁的中五毕业生阿Dan表示，现时正于护理安老院任职活动工作员，月薪8000元，但中三毕业的只有6000元。他表示，他的职位将于明年3月到期，但政府未有公布会否继续开设这个职位，令他感到前路茫茫，「如果到期又唔再请人，会影响机构服务质素」。<br />
阿Dan表示有调查指出，大多数活动工作员都想在小区服务行业发展，惟他们学历一般，希望进修提升技能，但部份机构工时不定，且会因为他们是短期合约员工，把进修资源都拨予长工，间接抹煞他们自我增值机会。阿Dan希望政府设立长工职位，令活动工作员就业得到保障。<br />
本报记者<br />
<br />
个案二：电梯工人　压力爆煲<br />
<br />
打工仔返工放工乘搭的电梯，有赖电梯工人定时检查及维修。电梯业总工会副组织主任李炳权表示，标准「人机比例」为一比五十，即每更有两个人，每两星期负责50部电梯保养，如抹油、检查钢缆、清理电梯槽等，每部电梯需时约1.5小时至2小时，惟大部份维修公司人手短缺，现时人机比例竟是一比八十，变相令每部电梯的保养时间缩短一半至45分钟，保养质素大受影响，间接威胁电梯乘客安全。<br />
电梯业总工会昨有30多人参与工联会5.1游行，希望政府制订人机比例标准，及要求维修公司增聘人手，纾缓工人压力。此外，李炳权指有部份工人入行四年，已获维修专业认可，但月薪只有7000多元，要求政府正视专业工人低薪问题。<br />
本报记者<br />
<br />
个案三：合约邮差　渴望长工<br />
<br />
现时邮政署有约2200个临时合约员工，他们的工作性质与公务员无异，但薪金少一半，每年续约前更提心吊胆，准备随时不获续聘。香港邮政合约员工权益会20多个临时合约员工，昨参加街坊工友服务处5.1游行，要求同工同酬。<br />
于中环邮政总局工作5年的梁先生表示，去年的合约无故多加两个条款，包括「会更改你的实际工作时间及/或地点」，以及「你所受聘的为有时限的临时职位」。他认为「随时改时间地点，同公务员无分别」，但同工不同酬，临时合约员工每月工资8000多元，而公务员则可达2万元，炮轰署方不公平，且条款有暗示不续聘约满员工之嫌。工会望署方一视同仁，杜绝同工不同酬。<br />
本报记者</div>

							
							
							
							
