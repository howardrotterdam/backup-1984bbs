<br />
<img src="http://s1.postimage.org/NV3vJ-5f35d1fb8e4107a922b29c4cd7ac8215.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
警方向人群射水炮，多人走避不及受伤。路透社<br />
