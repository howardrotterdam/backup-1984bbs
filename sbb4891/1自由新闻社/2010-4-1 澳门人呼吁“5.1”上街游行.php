民主起动、澳门民生协进会、博彩及建筑业联合自由工会等12个工会、团体，联同一批80后、90后青年，昨日分别在澳门北区三角花园及佑汉公园出发，准备到南湾区的政府总部递交请愿信。大约有200名争取放宽超龄内地子女到澳门团聚的游行人士，早于中午1时抵达政府总部时与警察发生冲突，他们不满只许由代表递交请愿信，一度冲击铁马及坐在行人路上，最后经警员劝离开或遭抬离现场。<br />
<br />
