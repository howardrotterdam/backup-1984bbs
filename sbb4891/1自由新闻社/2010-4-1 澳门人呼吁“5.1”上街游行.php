<br />
大约3时许，游行人士经提督大马路走到沙梨头海边街附近的水上街，遭警方架起的两重铁马及大批配备盾牌的防暴警察阻挡。示威者上前冲击撞散第一重铁马，警方马上出动水炮及胡椒喷雾驱赶示威分子。人群执起路边的石头或示威横额的竹枝投向警员，其间不断挥动国旗及播放国歌打气，有示威者大叫：「我O点解会罴ざ，你试吓臃箍滋味就知点解罴ざ。」<br />
<br />
警方随后增派防暴警察到场，前后共派出警力350人，扩大封锁范围，又派便衣混入示威人群当中，但遭识破而被围殴，有女记者采访时则被水炮击中倒地受伤，场面异常混乱。双方冲突期间至少有41人受伤，当中包括32名警员及两名记者，部份示威者血流披面及晕倒。游行人士不断冲击警方封锁线，双方对峙至深夜。<br />
<br />
有居住在水上街附近的澳门居民拍下冲突场面，并将图片放在讨论区，引起激烈讨论，不少网民批评政府做法不对，「政府成日讲S都无法可依，点样可以话畀人知O做事系依法？难道回归10年，官员仲系童子军？」<br />
<br />
在示威队伍现场的澳门民主派议员吴国昌及区锦新，批评警方处理手法不当，警方使用胡椒喷雾和水炮容易伤及民，而冲突期间，警方并没有高层人员在场向示威者解释事件，只是不断驱赶，认为做法只会刺激游行人士的情绪。<br />
<br />
