吴国昌表示，特首崔世安上场后，政府仍漠视劳工问题，黑工问题依然严重，民怨日深，工人们忍无可忍，遇上警方阻挠，加上警方态度强硬，终爆发今次警民冲突。<br />
<br />
在场的博彩及建筑业联合自由工会理事长伍锡尧表示，07年5.1游行曾爆发警民冲突，绰号「抽水哥」的澳门治安警员向天开枪示警，但那次游行警方最后出了封信，要求游行人士离去，今次警方却完全没有理会示威者的诉求，只是不断呼喝示威者离开，未有派出代表与示威人士商讨解决。<br />
<br />
身在上海出席世博活动的崔世安通过电话，向代理行政长官陈丽敏及保安司司长张国华，了解示威人士的诉求及游行秩序，对有人受伤表示关切和慰问。澳门政府表示，警方为维护公共秩序，在事件中及时采取措施，尽力将影响减至最低，重申鼓励市民循各种正当渠道发表意见和反映诉求；惟今次游行中，部份人士不按警方指示的游行路线，引起冲突。<br />
