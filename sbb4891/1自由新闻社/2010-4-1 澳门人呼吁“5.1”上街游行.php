<img src="http://s1.postimage.org/NVkYJ-5f35d1fb8e4107a922b29c4cd7ac8215.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
澳民警方晚上加强布防，气氛紧张。区民杰摄<br />
<br />
有澳门学者指出，澳门5.1游行已演变为香港7.1游行的翻版，成为澳门人每年宣泄民怨的途径。而黑工、贪污及高楼价等社会问题形成了澳门的深层次矛盾，就算行政长官每年派钱，都无法掩盖问题的严重性。<br />
<br />
澳门行政长官崔世安在3月的施政报告中继续派钱，每名澳门居民可获澳门币6000元。而100多名建筑工人上月12日到澳门劳工局示威，爆发警民冲突，劳工局长孙家雄在事后也宣布了一系列安抚措施，包括打击黑工及加强本地工人培训等，但到昨日仍然爆发严重冲突。<br />
<br />
楼价高企无法安居<br />
<br />
澳门大学社会科学及人文学院助理教授林玉凤表示，令建筑工人长期失业的黑工问题已缠绕了10年，很多澳门人都有切肤之痛，开始怀疑政府是否真心打击黑工。另一方面，楼价近年不合理地急升，令澳门人无法安居，不少人也觉得高楼价与欧文龙案爆出的贪污黑幕有关，加深了澳门人的愤怒。5.1游行已变得和香港的7.1游行一样，成为民怨每年的宣泄途径。另外，近年多了年轻人参加游行，表达不同的诉求，例如今年的主题是反河蟹，认为今次澳门政府为了面子，武力禁止游行人士经过新马路，只会进一步激化不满情绪。<br />
<br />
澳门立法会议员区锦新指出，澳门不少地产商都是行政长官选委会的成员，对政府有很大影响力，所以澳门劳工局所谓打击黑工，只是每天派人到地盘象征式点人数，人人都知道是「做戏」。5.1游行自07年发生抽水哥开枪事件后，原本已有默契，游行会绕过新马路，但过去几年政府完全没有真正改善黑工问题，令工人不满日益加深，终于在今年再爆冲突。<br />
<br />
本报记者</div>

							
							
							
							
