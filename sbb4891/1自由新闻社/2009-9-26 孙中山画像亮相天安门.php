<br />
孙文越飞宣言 <br />
<br />
孙文越飞宣言：1922年陈炯明政变后，孙中山从广东退居上海。<br />
<br />
苏俄代表越飞在北京，同上海的孙中山的代表，互相访问和交换书信。孙中山曾派张继去会见越飞。经过张继的联系，1923年1月17日，越飞从北京南下，来到上海，去莫利爱路29号孙中山的住宅，首次同孙中山会面，倾谈颇久。此後，双方经过几次会谈，于1月26日联名发表了著名的《孙文越飞宣言》： <br />
<br />
“越飞君此次在沪，曾与孙逸仙博士谈数次，关于中俄关系各重要事件，意见一致，而以下列数端尤著： <br />
<br />
（一）<strong>孙逸仙博士以为共产党主义组织甚至苏维埃制度，事实上均不能引用于中国</strong>，因中国并无可使此项共产主义或苏维埃制度实施成功之情形存在之故。此项见解，越飞君完全同感，且以为中国最重要最迫切之问题，乃在民国的统一之成功，与完全国家的独立之获得。关于此项大事业，越飞君并向孙博士保证，中国当得到俄国国民最挚热之同情，且可以俄国援助为依赖。 <br />
<br />
（二）为明了此等地位起见，孙逸仙博士要求越飞君再度切实声明1920年9月27日俄国对中国通牒中所列之原则。越飞君当即重新确认此项原则，并向孙博士切实宣称：俄国政府准备且愿意根据俄国抛弃帝政时代对华一切条约及强夺权利之基础，另行开始中俄交涉。上述各条约中，包括关于中东铁路之各项条约及协定在内（关于此路之管理，上述通牒中第7条曾特别叙述之）。 <br />
<br />
<br />
（三）因承认全部中东铁路问题，只能于适当之中俄会议始克满意解决，故孙逸仙博士以为就目前的实际情况，宜于该路之管理之上，觅一相当办法，且与越飞君同意现行铁路管理法，只能由中俄两国政府不加成见，协商暂时改组。但不得损害两方之真实权利及特别利益。同时，孙逸仙博士以为此点应与张作霖将军商洽。 <br />
