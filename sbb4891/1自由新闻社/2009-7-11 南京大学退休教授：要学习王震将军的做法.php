													<h2>南京大学退休教授：要学习王震将军的做法</h2>
						
						
																											<div id="postmessage_158524" class="t_msgfont">中国必须摈弃前苏联的民族政策<br />
<br />
● 汪应果<br />
<br />
中华民族在长期发展的过程中，正是因为各族群的大融合，<strong>才造就了这个民族总体上基因较好、智商较高的优势</strong>。现在人为地分隔族群，简直是逆历史潮流而动。<br />
<br />
汪应果（澳门）<br />
<br />
７月5日，新疆地区发生维族殴打、杀戮、焚烧汉人的骚乱。维族人一直在广大汉人的心目中形象相当美好，被认为是中华民族中长得最美丽的族群之一，这一来，形象大打折扣了。<br />
<br />
　　发生这样的事，固然有很多客观因素，但归根到底，不能不反思我们长期执行的所谓“民族政策”是否从理论上就有错误。<br />
<br />
　　一年多以前，当西藏拉萨发生骚乱时，我就在博客上发表了一篇文章，题目是《中国只有一个民族――中华民族》，就指出了中国长期推行的民族政策的理论错误，但政治家显然不屑看我们小民的意见，于是一年后，相同的悲剧重演，只是地点换了新疆。<br />
<br />
　　在那篇文章里，我从美国的经验里得出一个结论，那就是中国长期从前苏联斯大林那里学来的所谓“民族自治制度”是不正确的，原因是我们把“族群”或“族裔”（race）错当成了“民族”（Nation），于是自找麻烦，种下今日之苦果。<br />
<br />
　　为了说明这一点，我把原先的文章再发一遍：<br />
<br />
必须加强大熔炉力度<br />
<br />
中国历史上早就形成了一个统一的民族国家，中国只有一个民族(英语是Nation)叫“中华民族”。长期以来在基本理论上犯了知识性错误，沿用前苏联的概念，结果造成许许多多不必要的问题，埋下了借所谓“民族问题”从事分裂祖国活动的隐患，前苏联解体的教训是我们的前车之鉴。<br />
<br />
现在的56个民族应称之为“种族”或“族裔”、“族群”（英语是Race），他们在中华民族这个大家庭里一口锅里轮饭勺，理应具有共同的价值观。因此过多地强调“族群差别”是不对的。<br />
<br />
　　“民族”（nation)与“种族”（race)是两个不同的概念，在英语里nation具有“民族”、“国家”两个解释，说明同一个国家就是同一个民族的意思。<br />
<br />
　　在一个统一的民族国家内，各族裔的历史趋势应该是：加速融合的过程，就像美国那样，是大熔炉。在美国黑人与白人的矛盾很深，但黑人不是“民族”，统称“ 黑人”；同样，中国人移民美国就不称“中华民族”了，统称“华人”、“华裔”。中国也应如此办理，统称“汉人”、“藏人”、“蒙人”等等，这里不存在民族矛盾，只有族群差别。这就跟平时说的“上海人”、“北京人”、“江苏人”、“客家人”等等是一样的。<br />
<br />
　　中国必须加强大熔炉的力度，坚决搞大同。<br />
<br />
　　中国“民族委”是否该考虑换个名字？所谓“少数民族”政策是否有重新审视的必要？<br />
<br />
　　表面上看是两个概念的混淆，实际上是认识出了问题，它造成的错误是：<br />
<br />
民族自治政府种下祸根<br />
<br />
　　一、概念混淆的结果，是成立了许许多多的“民族自治政府”，这一来不是缩小族群的差异，而是扩大甚至是把差异从此凝固化、永久化。因为“Nation”又有“国家”的含义，于是就为所谓的“民族”分裂分子提供了“独立建国”的理论基础。<br />
<br />
　　二、在民族自治的基础上又提出了有“56个民族”，这又是一个不科学的提法，又是一个分隔族群反对族群融合的提法，我不知道“民族委”以什么标准来划分 “民族”？以血统吗？那么请问“汉族”难道是一个血统吗？所谓“汉族”本身就是一个大杂烩，它跟比较单纯血统的“维族”“哈族”等等不是一个概念，它们概念的内涵不在一个层次上。<br />
<br />
　　笔者本人头发就是自来卷，笔者的母亲、孩子、旁系亲属的后代统统是自来卷，以致几家人的后代（包括重孙女）不久前几乎不约而同都提出了一个共同的问题：“妈妈，我们有外国人的血统吗？”“为什么别人都说我们像外国人？”但我们是“汉族”。<br />
<br />
我的回答是：“说不定我们的祖先有哪个小伙子唱着‘胡姬貌如花，当户笑春风，’看上了一个‘当垆胡姬’也未可知。没准我们的老祖宗就是一个维族姑娘。”今天的汉族可说是早已融合了几十个上百个不同的血统了。<br />
<br />
　　再说，所谓“56个”，那么澳门葡萄牙人的后代、香港英国人的后代有的加入了中国籍，为什么不算进去？<br />
<br />
　　三、一个国家都是一个统一的民族，族群融合是他的大趋势，这是顺历史潮流而动。中华民族在长期发展的过程中，正是因为各族群的大融合，才造就了这个民族总体上基因较好、智商较高的优势。现在人为地分隔族群，简直是逆历史潮流而动。<br />
<br />
　　四、秦始皇纵有万般罪恶，但他的“书同文”，保证了中华民族不像欧洲那样分裂成几百个公国，使欧洲人民饱受几个世纪的战争之苦。这里一码归一码，功过要分清。而今天的“民族自治政府”则种下了中国日久分裂成56个国家的祸根。<br />
<br />
调整“族裔政策”<br />
<br />
　　鉴于以上几点，中国必须重新认识、重新调整“族裔政策”，我的建议是：<br />
<br />
　　一、重新对全民族进行一次“民族”与“族裔”的知识教育，把理论搞清楚，统一思想。<br />
<br />
　　二、<strong>撤销各级“民族自治政府”，</strong>各族群没有独立的政府权力，都以个人身份加入公务员的队伍，参与政府管理，各族裔权利平等。这就像美国的赵小兰、骆家辉一样，他们没有华人的政府。这就为日后扫清了分裂的隐患。<br />
<br />
　　三、对少数族裔集聚的地区，进行经济上、开发项目上的倾斜，发展他们的经济，让他们渐渐跟上汉人的发展步伐，渐渐富起来，但取消其他比汉人高一等的待遇。少数族裔必须认同“归化”，就像美国、澳洲等发达国家一样，必须向主流族群归化认同，<strong>必须学中国话，否则不能享有中国公民待遇。</strong><br />
<br />
　　四、鼓励并支持少数族群保留自身文化传统，<strong>但同时推进各族群婚姻大融合，推进各族群进入人口较少的地区，搞大移民，大混合。</strong><br />
<br />
<strong>五、学习当年王胡子王震将军的做法，让少数族群中的某些人又敬又畏。</strong><br />
<br />
　　对此次打砸抢坏头头坚决杀无赦，对罪行较轻的能不杀就不杀，但要进行终身管制，一律分散移民至汉族地区，进行消化瓦解。这方面可学美国警察的做法，对有轻微偷窃的人，警察每晚必上门天天教育。<br />
<br />
　　只要坚持这样做下去，中国的所谓“民族问题”可大定矣。<br />
<br />
