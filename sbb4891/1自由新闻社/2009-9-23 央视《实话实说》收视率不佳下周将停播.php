																											<div id="postmessage_253457" class="t_msgfont">央视《实话实说》收视率不佳下周将停播<br />
<a href="http://www.sina.com.cn" target="_blank">http://www.sina.com.cn</a>&nbsp;&nbsp;2009年09月23日04:43&nbsp;&nbsp;东方网 <br />
<br />
　　《实话实说》将告别观众。从央视新闻频道的改版中被“踢到”了央视一套，如今又将在央视一套改为高清频道的变革中被“淘汰”，正式退出荧屏。据悉，央视新一轮转高清改版工作，终于开始“动刀”，届时作为央视“王牌频道”的一套整体节目编排将有较大的调整，《艺术人生》等六档央视的金牌栏目将聚集在一套，而《实话实说》节目却由于收视率不理想而将于下周起停播，告别荧屏。9月26日晚17：23播出的将是其最后一期节目。<br />
<br />
　　收编《讲述》等精品栏目<br />
<br />
　　之前改版带来的收视率提高，令央视尝到了甜头。记者获悉，新一轮的改版将从本月28日起，并将从央视一套开始。<br />
<br />
　　改版后的一套将全部改为全高清信号播出，有12档栏目将退出节目单，转而收编其他各频道的六档精品节目到旗下，力图打造“王牌频道”。<br />
<br />
　　据了解，本次央视为一套制定的改版目标为“坚持新闻主线，形成与CCTV新闻频道差异化，同时将汇聚央视全台的精品栏目，在节目编排上形成形态各异的品牌形象”。已经确定吸纳到一套的节目包括文艺中心的《开心辞典》、《非常6+1》、《艺术人生》、《快乐驿站》，体育中心的《城市之间》，青少中心的《智慧树》，以及原定在央视十套首播的《讲述》和《探索发现》节目。据知情人透露，这些节目几乎都是央视各个中心的精品。<br />
<br />
　　对《实话实说》不留情<br />
<br />
　　据悉，从28日起被清出央视一套的12个节目分别是《今天故事汇》、《实话实说》、《电影传奇》、《健康之路周刊》、《半边天》、《道德观察》、《走近科学》、《正大综艺》、《九州大戏台》、《大风车》、《三星智力快车》、《我们》。其中《正大综艺》、《实话实说》、《大风车》等都是央视的老牌节目。<br />
<br />
　　记者从知情人处获悉，此次改版几乎是“六亲不认”，就算是《实话实说》也未能幸免。不过，并非所有被踢出一套的节目都会面临“下岗”，有一些节目会调整到其他频道播出。但据可靠消息称，《实话实说》会彻底告别荧屏。“一方面，这个节目是从新闻频道调过来的，但新闻频道的改版已经比较完整了，不可能再回去。另一方面，《实话实说》现在的收视率大不如前，产生的话题性和关注度也越来越不尽如人意。”所以，在央视一套“打造精品”的主题之下，处境尴尬的《实话实说》只好与观众再见。(作者：林艳雯)<br />
