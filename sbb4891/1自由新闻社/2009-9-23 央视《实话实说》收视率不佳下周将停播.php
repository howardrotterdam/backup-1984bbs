<br />
徐先生：“我作为一个在电视台干过、在报社也干过的十几年的记者，收视率这个东西一直是一个让内行看着头痛，让外行看不懂的一个术语。其实这个收视率有多少公正？我说不清楚，但是我能说清楚的是《实话实说》过去曾经有过辉煌，但在它最辉煌的时候，它把崔永元给调走了，换了。换了之后，《实话实说》这个节目收视率有所下降，但这几年《实话实说》里面的有些内容，我有时候也做了一些关注，我就发现它的话题有点不是太像刚开始创办这个栏目的时候那么锋芒毕露，包括焦点访谈，还有新闻调查。这几个栏目的一些记者包括一些知名人和我们都是朋友，我们也常在一起跟他们讨论这些话题。他们说：‘人在江湖，身不由己’。这个话呢我们大家也可以好好的玩味一番，只可意会，不可言传。但是我感觉《实话实说》倒掉就像雷锋塔倒掉一样，很值得纪念。其实我感觉《实话实说》倒掉还不如中央电视台一起倒掉，你皮之不存，毛焉能附？中央电视台它垄断了一个国家的新闻资源，然后做了一些不是新闻的新闻。它把自己一个垃圾，就是过去是一个曾经辉煌的栏目，现在变成垃圾栏目倒掉，我感觉到不值得我们太多的关注。倒掉就倒掉是一件好事。”<br />
<br />
记者：“那么这个《实话实说》节目的主持人何静今天在她的博客上写了一篇题为《别拿收视率说事》的文章，来回应她的栏目被停播的事情。她其中有这么一句：‘一个亲爹不要，后娘不疼的孩子是孩子自己的错吗？这该怎么意会呢？徐先生？”<br />
<br />
徐先生：“据我所了解的情况，这个栏目刚创播的时候，曾经赢得了很多的赞誉，包括一些领导人也获得过辉煌，也获得过很多奖励。就像她说的，亲爹不要了，后娘她也不可能要。而且每个官员这个台长也换了好几任了，上来之后这些地方官员他都要大兴土木，然后表示自己的政绩。你像那个《咏乐汇》，甚至把《幸运52》都合并在里面去了。他为什么搞咏乐汇呢？其实领导下来之后呢，屁股决定脑袋，他感觉到拍脑门子的工程更能显示出自己的政绩，这是我干的。我干得再好，《实话实说》是其它台长在任的时候补抓的，我是为他人做绣衣，所以他感觉不如自己另起炉灶。《实话实说》的主持人写了一次博客，这个博客我没看，但是我可以推断这个主持人他讲了那些话之后催她下岗。”<br />
<br />
<br />
记者：“好，那凌先生你怎么看？”<br />
凌沧洲：“下岗？可能不见得吧？因为现在实际上是个栏目停掉了，可能对他自己在台里的地位肯定是有影响的了。有人发牢骚，有人欲说含羞，犹抱半弹琵琶半遮面呀。这个牢骚是可以理解的。我觉得从相对的来讲，《实话实说》这个栏目比起其它假、大、空的栏目更加假、大、空的栏目，还是具有一些观众的缘吧。我觉得也体现了央视它自身确实有危机感，存在着各种舆论压力。它也想做一些姿态，做一些秀来适应市场的压力和舆论的批评吧。但是只要它的宣传的性质不变，还是高高在上的，与人民群众的距离是非常遥远的。”<br />
<br />
记者：“那既然这样讲，我又回到第一个问题有关了，因为国内有一个博客在博客中国上刊登说这个《实话实说》的栏目被停播是因为的确不能说实话，所以被停播。因为中国这个制度和体制不允许它说实话，他做了一个比喻就说像当初的俄罗斯‘消息报没有消息，真理报没有真理’一样。这个《实话实说》被停播的真正原因是什么？”<br />
<br />
徐先生：“这个栏目被停播，首先第一呢它是跟经济上能挂钩的，据我所了解的一些皮毛，就是因为现在它《实话实说》受到限制，对一些敏感的话题不让它涉及，涉及之后呢导致了对这个栏目的间断，没有大的广告商出产广告，并不是收视率的问题，有的栏目收视率很高，但是它的广告效益并不是太好，而《实话实说》这个栏目的存在如果不砍掉它更具备一种社会效益。如果说这个栏目，你央视领导人知道这个栏目收视率一直下降，你不应该把这个节目停掉，而应该去换主持人。”<br />
<br />
记者：“那既然这样讲，我再问一下凌先生，那么现在央视对停播这个《实话实说》，不能实话实说，那么你怎么看？你认为这个原因是什么呢？”<br />
<br />
凌沧洲：“我觉得确实很有可能收视率只不过是一个借口吧。我的意思就是说是整个的一个环境，时代的环境所决定的。就像希腊的哲人米兰德所说：‘许多人生来并不是无赖，但是他们不得不成为了无赖。’我的意思就说可能在一个封闭的社会，比较保守至少媒体还属于高度垄断不够开放的地方，那怕有一点点真实的声音可能也会让某些人心惊肉跳，也许这是真正的原因吧！”<br />
<br />
以上是本台记者闻剑与北京两位资深媒体人士凌沧洲和徐祥讨论央视《实话实说》栏目即将停播的原因。</div>

							
							
							
							
