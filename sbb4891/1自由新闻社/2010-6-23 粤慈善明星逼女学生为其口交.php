<br />
2008年2月，李明远投入30万元，在罗村罗湖中路租下了一间百平米的门面，办起了以“扶贫助学”为主题的爱心咖啡厅，同时作为罗村爱心俱乐部所在地。<br />
<br />
媒体曾报道，以爱心俱乐部为依托，李明远建立了爱心qq群，召集和组织义工活动。最高峰时，爱心队伍有百余人。同时，他还在咖啡馆内设了个心灵驿站，为孩子进行心理辅导。<br />
<br />
南都记者曾受邀，2008年中秋节与李明远一起探访孤儿，并在咖啡馆用餐。咖啡馆约200多平米，两层高，一推门，便可看到地上印着“拒绝冷漠”四个字。<br />
<br />
墙上贴满了从山区拍回来的孩子们上课、生活的照片，每张桌上都贴着公益宣传单，墙上还贴着红色标语，“我不是在炫耀，只是在呼吁，也是在呼唤”。李明远个人手机号码，作为“爱心帮扶热线”，也醒目标在门口。<br />
<br />
“餐厅的所有收入都用来献爱心”，李明远曾说，这就是他开店初衷，偶尔遇到困难家庭，他就让孩子来此打工。<br />
<br />
李明远自述，从开业起，咖啡馆每月赔本5000元，积蓄已所剩无几。本地人士证实，罗村城区三间咖啡馆，李明远的经营最差，经常门可罗雀。为了支撑餐厅，他重新做起装修工。<br />
<br />
有人说，李明远在宣传自己，为了咖啡厅的生意，从中牟利。面对媒体，他在2008年公开表示，从9月1日至15日，只要到农业银行向南海慈善会账户存15元，凭回执即可在咖啡馆享8.5折爱心消费优惠。<br />
<br />
有义工质疑作秀他自言并非炫耀<br />
<br />
“15年来的帮助，要坚持是不容易的，我不是在炫耀自己，只是希望你们无论怎么艰难也不要放弃助人的信念，爱心需要的是坚持，这是我对大家的期望！”李明远在个人慈善博客中写道。<br />
<br />
但在佛山义工圈，也有不少人质疑李明远爱作秀。多名义工表示，在媒体面前侃侃而谈的他，私下会抱怨这些钱捐出去都不知有什么用。有一次，罗村福利院要为他们颁发锦旗，一位义工还亲耳听到李明远嘟囔，不知道有没有记者去。<br />
<br />
一本地媒体记者证实，一次探访贫困家庭时，李明远打电话给记者，让对方尽快赶去拍他们“搬电视进屋”的镜头。<br />
<br />
知情人士透露，有时个别义工工作不够到位，李明远就会当着很多义工的面，大声责骂。在外点菜时，他有时也会对服务员大吼，指责其服务态度不好。<br />
<br />
佛山一家知名慈善组织人士明哥表示，每次去咖啡馆，李明远老说自己最近又做了什么事，又资助了几个孩子，很反感。<br />
<br />
南海亲子义工队队长张伊婷表示，就义工工作而言，李明远还是比较热心、比较用心的。义工小静(化名)对李的第一印象是：挺平凡的人，热心公益，不少义工支持他。一名不愿透露姓名的南海义工表示，听说李很爱做好事，很热心公益。<br />
<br />
受伤女孩拒帮扶高调探访惹争议<br />
<br />
有义工透露，李明远高调的探访活动有时受到当事人抵触。在帮扶罗村心理创伤女孩时，李多次带大批义工登门拜访，心理敏感的女孩一次次将伤痕暴露在众人面前，最后拒绝再接受李的帮扶。<br />
<br />
省内一家知名助学N G O人士称，受助青少年特别是高中生，自尊心很强，不希望家境困难的自己聚于闪光灯下，不希望个人资料公开，甚至反对接受捐助，这样只会加重他们的自卑和创伤。该助学N G O每次探访，基本只带三四个义工去，偶尔会邀请媒体参与，但“绝不会成群结队去”。<br />
<br />
但也有义工对李明远的高密度探访表示支持。李个人慈善博客记载，在一次探访福利院残疾孤儿时，当看到很多孩子身体残缺时，桂城职校的学生都哭了。<br />
<br />
也有义工表示，仅看受助家庭的照片，无甚感触。但当一次李明远亲自带她去山区探访时，面对多病贫困的家庭，义工们纷纷落泪，心痛之余自掏腰包捐助了几百元。<br />
<br />
腾房助孤儿读书他想做佛山丛飞<br />
<br />
公众场合，李明远多次谈到丛飞，表示要以丛飞为榜样，要用丛飞精神激励自己。这位曾经的深圳义工联艺术团团长，十年间不惜借债捐出近300万元，认养孤儿32人，捐助失学儿童和残疾人146人，自己患胃癌时却付不起医疗费。<br />
<br />
“从1995年捐钱给同学治病和照顾其母亲，到今天资助15名特困孩子就学读书，13年来李明远一直在坚持着自己的爱心事业：作为村民代表，他每年都捐钱给村里孤寡老人和单亲家庭。”在媒体报道中，李明远家里洗衣机用了10多年，外壳已破损，仍没更换。<br />
<br />
在媒体报道中，李明远做出过类似丛飞的“疯狂”举动。2006年底，他与广宁县妇联联系，短短一年里，资助了15名孤儿和特困孩子重返校园，并将6人接来佛山，其中3人安置在李自己买的房子中，而他和妻儿则搬去与父母同住。<br />
<br />
对此，李的父母曾经不解，妻子也没少跟他起争执。义工小静说，李跟妻子一起参加义工活动，很恩爱。也有义工表示，他妻子从不参与义工聚会，走过去也不和李打招呼。<br />
<br />
李明远曾表示，虽然1/3的收入都用于这些孩子身上，但自己获得了心灵的快乐，这是任何东西都无法比拟的。他要一如既往做孩子们的“好哥哥”。<br />
<br />
事实上，丛飞的做法在民间慈善义工界争议很大。“这只会让爱心人士望而却步。”广州一家知名民间助学机构负责人曾提到非常反对丛飞的做法，“量力而行的爱心才是我们所应该提倡的爱心，才是能大众化的爱心”。<br />
<br />
李明远说<br />
<br />
15年来的帮助，要坚持是不容易的，我不是在炫耀自己，只是希望你们无论怎么艰难也不要放弃助人的信念，爱心需要的是坚持，这是我对大家的期望！<br />
<br />
媒体说<br />
<br />
他不是亿万富翁，却长期四处资助他人；他不是房产大亨，却专门腾出房屋给陌生人居住。<br />
<br />
义工说<br />
<br />
挺平凡的人，热心公益，就义工工作而言，他比较用心。<br />
<br />
他在媒体面前侃侃而谈，私下会抱怨这些钱捐出去都不知有什么用。他高调的探访活动，有时受到当事人抵触。</div>

							
							
							
							
