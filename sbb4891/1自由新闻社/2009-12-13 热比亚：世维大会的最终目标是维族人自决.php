12月10日是世界人权日，世界维吾尔人大会主席热比亚应国际人权组织大赦国际的邀请来巴黎参加活动。周四下午，热比亚在大赦国际巴黎分部接受了我们的采访。她重新回顾了几个月前的“七-五”事件，指出，是中国政府通过洗脑，煽动汉族人的民族情绪，激化新疆维汉两大族群间的矛盾。在她看来，“七- 五”事件是中央政府的阴谋，是政府的不作为使矛盾扩大。她指出，7月5日，出现维族人追打汉人事件；7月6日，出现汉人追打维族人。但是，在7月5日之前，即没有汉族人杀维族人，也没有维族人杀汉人。<br />
<br />
热比亚希望能够与中国政府谈判解决新疆问题。她表示，中国政府在宣传和平，而维吾尔人实实在在地需要和平，维族人不会向中国政府提出不可能的要求。世维大会的最终目的不是维吾尔人独立，而是自决。<br />
<br />
流亡海外藏人虽然也有不同的组织，但整体来说，他们以宗教信仰团结在一起。维族人在世界各地也有很多团体，世界维吾尔人大会靠什么来将这些维族人团结起来？<br />
<br />
热比亚回答说：“靠我一个母亲”。她解释说，维族人如今意识到他们需要一个母亲，一个领袖。如今各地51个维吾尔人组织已经与世界维吾尔人大会团结一致，她会带领维族人走向自由与和平。但她会以和平争取和平，索取爱，也给予爱。<br />
