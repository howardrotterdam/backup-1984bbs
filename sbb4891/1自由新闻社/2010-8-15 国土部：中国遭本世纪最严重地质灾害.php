20100814 16:08:57&nbsp; &nbsp;&nbsp; &nbsp;<br />
 <br />
（中央社台北14日电）中国国土资源部官员今天表示，今年中国重大地质灾害发生的频率与次数、造成死亡失踪的人数，都是本世纪以来最严重的一年。<br />
<br />
大陆媒体从8日发生的甘肃舟曲特大土石流灾变开始，就陆续报导各地的地质灾害意外，包括甘肃天水和陇南土石流致30人遇难32人失踪、四川绵竹暴雨引发地质灾害7人遇难、汶川暴雨多处地质灾害造成映秀镇32人失踪等。<br />
<br />
国土资源部地质环境司副司长陶庆法向中新社说明今年1到7月以来死亡10人以上的各项地质灾害。<br />
<br />
他说，全中国今年1到7月共发生14起10人以上死亡失踪的重大地质灾害事件，是近10年来最多，共379人死亡失踪，已经超过2001年以来任何一年全年的重大灾害造成的死亡失踪人数。<br />
<br />
这些地质灾害主要是由降雨所诱发，灾区大多是在地形陡峭、沟谷纵横、坡度大、岩石风化破碎的区域，且防范难度很大，大多数地区又是非监测点。<br />
<br />
陶庆法坦言，目前中国地质灾害防治除气候、地震等因素外，社会不重视、防范意识薄弱也是主因，很多县甚至连一个专职人员都没有。<br />
<br />
当局将1640个山区县做为防范地质灾害的重点地区，但投入的人力物力有限，还有很多地方没有地质灾害机构，既有的机构分析、调查、预警能力薄弱。<br />
<br />
国土资源部地质灾害防治应急办公室副主任田廷山也提出动员社会力量参与防治的重要性，要求大家在有地质灾害的潜藏地，一旦遇到极端天候，就应及时启动应急响应，立刻疏散当地居民。<br />
<br />
据中国地质环境监测院、国家地质调查局数据，全中国至少还有1.6万个与甘肃舟曲同属「特大型」或「重大型」级的地质灾害潜藏区。<br />
<br />
