<br />
如果我们仔细观察不同文化下的话语风格，是否讲求逻辑也许是其中值得关注的一个分别。唐德刚先生曾经分析我们的思想史，其中不讲逻辑的特征被他归因于“东西两方对‘法’的观念不同的滋长有以致之”。他说：<br />
<br />
 <br />
<br />
“法律”是最讲逻辑的。因而个个律师都是逻辑专家；而律师在西方社会里的地位――从古希腊罗马到今日的英美法苏――那还了得！可是我们传统中国人（古印度人也是一样）最瞧不起所谓“写蓝格子”的“绍兴师爷”和“狗头讼师”。我们的“仲尼之徒”一向是注重“为政以德”的。毫无法理常识的“青天大老爷”动不动就来他个“五经断狱”，断得好的，则天理、国法、人情、良心俱在其中；断得不好的，则来他个“和尚打伞”，无法（发）无天，满口的革命大道理，事实上则连最起码的逻辑也没有了。西方就适得其反了。西方的律师，诉讼起来，管他娘天理、人情、良心，只要逻辑不差，在国法上自有“胜诉”。因而他们的逻辑，也就愈发展愈细密了。（《胡适口述自传》第五章注释23）<br />
