 <br />
<br />
唐先生的说法也许多少忽略了一国政治形态对于逻辑学发育的影响，例如雅典式的民主政体对于演说、辩论的讲求，就在很大程度上推进了逻辑学的发展。不过，说法庭之上是否讲求法律至上、遵循逻辑规范是其中重要的因素却毫无疑问。<br />
<br />
 <br />
<br />
眼下全国司法界正在“大学习、大讨论”，其中的核心内容就是贯彻“三个至上”学说：始终坚持党的事业至上、人民利益至上、宪法法律至上。报章连篇累牍，会议连绵不绝，最高法院甚至还组成宣讲团在全国巡回宣讲。我对此感到很困惑，因为这“三个至上”在逻辑上存在着值得商讨的地方，尤其是把它运用到司法领域时，这样的逻辑困难会给司法界带来某些混乱。<br />
<br />
 <br />
<br />
很明显，所谓“至上”，便是最高的准则。至上有其惟一性，不可以有多个准则一块儿至高无上。好比在一个家庭里，假如有一个人说话具有最终极的权威，那么就不可能是公公的意志至上，婆婆的意志同样至上，媳妇的意志还是至上。又好比奥运会比赛，冠军只能有一个。一下子出现三个冠军就摆不好――领奖台齐平，同时奏三个不同国家的国歌，岂不是乱了套，或者用时髦的说法，就要“俯卧撑”，或者如正龙拍虎一般扑朔迷离！因此，如果党的事业、人民利益以及宪法法律是具有不同内容、相互区别的三个事物，那么我们必须给出一个顺序，按照法律界熟悉的说法，要对于三者的效力位阶作出安排。例如，或许可以如此表达：当法官处理案件时，需要对于上述三者进行考察，当三者统一时，应依据宪法和法律作出判决；当三者有矛盾时，应将人民利益置于宪法法律之上，将党的事业置于人民利益和宪法法律之上。也就是说，在效力位阶上，党的事业最高，人民利益次之，宪法法律再次之。<br />
<br />
 <br />
<br />
这样的说法在逻辑上没有问题了，但是在我们的政治语境下却是不成立的。因为按照官方一贯的话语模式，在我国，这三者是一而三、三而一的。十七大修改通过的《党章》在总纲里明确地说：“党除了工人阶级和最广大人民群众的利益，没有自己特殊的利益。”在一次领导人的讲话中，我们读到这样的说法：“我们党领导人民制定了宪法和法律，也要领导人民遵守宪法和法律。宪法和法律体现了党的主张和人民意志的统一。党员遵守宪法和法律就是遵从人民的意志，服从党的领导。所有的党组织、党员尤其是负责干部的言行，都不得同宪法、法律相抵触。加强党的领导同充分发扬民主和坚持依法办事是一致的。”（《十三大以来重要文件汇编》中册页943-4）既然三者之间毫无区别，那么不厌其烦地把三者并举，而且要“三个至上”，岂非修辞上的同语反复？<br />
<br />
 <br />
<br />
问题的困难还在于，如果用“三个至上”作为指导司法的准则，那么司法官员就必须在每一个案件里对于三者作出辨析，要时时分析，怎样的判决不损害党的事业，怎样的判决符合人民利益，当然还需要符合宪法和法律。对于我们的数量在20万以上的法官来说，这似乎是一个大难题。对于什么是党的事业，什么是人民利益，不同的法官也许在理解上会有很大差异。张志新对于“文革”提出异议，她的观点尽管与当时的党中央决策完全矛盾，但是后来的历史证明，如果她的主张得到采纳，党的事业就会得到维护。当时的司法机关判处张志新死刑，这种表面看来符合中央要求的做法，最终证明给党的事业造成了巨大的损失。再说人民利益，所有的诉讼都存在着对立的利益诉求，农民状告县政府违反土地承包合同，“最牛钉子户”因补偿不合理拒绝拆迁，提款机出问题许霆飙取17万先被判刑无期后来又改为5年徒刑，刘燕文诉北京大学要求颁发毕业证先胜而后输……究竟谁是人民，谁是敌人？判谁胜诉算是“人民利益至上”？如果放开手让不同的法院、每一个法官都按照各自对于党的事业、人民利益的理解而不是严格地依照宪法和法律的规则去办案，这个国家的司法岂不是要各吹各的号，各唱各的调，燕瘦环肥，五花八门？<br />
<br />
 <br />
<br />
自己越想越不解，“一事不知，士之耻也。”遵循进一步解放思想的教导，提出上述疑问，尚祈高人为我释疑解惑，则幸甚。</div>

							
							
							
							
