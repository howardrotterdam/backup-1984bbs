奇事：在中国银行买的黄金神奇地生锈了,福娃的裆烂了(图)<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;2008年7月，我在中国银行福州晋安支行购买了五福同堂这款由深圳东方金钰珠宝实业有限公司生产的奥运千足金限量珍藏版纪念品，本来是一件很快乐的事情，但是到了2009年10月1日就快乐不起来了，因为千足金居然生锈了。更让人郁闷的是中国银行和深圳东方金钰珠宝实业有限公司在推脱了大半年之后竟然说是生产工艺的问题，给出3个意见1.以原价退回;2.拿回去厂家清洗;3.换货。但是仔细想下，原价退回的话，损失可就大了，一方面黄金价格现在已经不是原来的价格了，一方面奥运结束之后，限量珍藏的东西已经升值，所以这个意见无法接受;如果拿回厂家清洗的话，也不合适，因为洗了之后难道就不会再生锈了吗，难道要定期去清洗吗，所以这个意见也无法接受;如果是换货的话，想想也不对头，因为限量的东西都卖完了，怎么还有东西可以换呢，如果可以换，这不就是明显忽悠消费者吗。 <br />
<br />
&nbsp; &nbsp; 直到现在，中国银行和深圳东方金钰珠宝实业有限公司还无法给出一个让我们满意的处理方案，而且态度越来越差，让我对他们失望透了。当初中国银行负责人口口声声说他们也是受害者，但是作为大型企业，为什么就不能主动去维护消费者的利益呢？作为一家银行，作为一家大公司，连这最基本的诚信和责任都做不到，又谈什么发展，消费者又怎么能相信你们呢。本来我们国家举办奥运会，我们感到无比自豪，想留下奥运会的一些痕迹，给下一代保留一些关于奥运的东西，见证中国百年盛世，所以才买了这样一件奥运限量珍藏的纪念品，又因为确信黄金的稳定，才决定买金娃赐福这款是千足金的奥运纪念品。 <br />
<br />
&nbsp; &nbsp; 前人总说“花钱买教训”，也许就是这个意思，今天，我一个人扔了这个钱当了次笨蛋，但是，如今，我要当个勤劳的笨蛋，到全国各大网站发贴去，只希望大家都能当个理智的消费者。<br />
