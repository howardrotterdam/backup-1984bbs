													<h2>风水在北大是科学</h2>
						
						
																											<div id="postmessage_391571" class="t_msgfont">科学新闻12月23日报道 北京大学的门岗查证很严，但这并未妨碍风水先生走进北大。蔡元培提出的“兼容并包”理念今天演绎成了高校风水课。<br />
<br />
11月22日晚，大冷的天，黑漆漆的夜。二教，这座北大最宽敞豪华、可抵御八度（烈度）地震的教学楼。黑板上，粉笔字歪歪斜斜地写着“从科学视角解读风水”，似乎感觉这个说法不够科学，少顷，主持人匆忙上前把题目改成了“从科学视角解读风水学”。坐在记者旁边的一位嘀咕，“一字之差，是不自信的表现吧？”<br />
<br />
讲座由北京大学易学社主办。如北大很多著名教授的讲座一样，胡一鸣的讲座尚未开始就已座无虚席，后来者只能坐在地板上，或者挤在角落里。<br />
<br />
非常之处是，除了捧着诸如习题集、英语备考书籍的学子，一些操着各式方言的人士也风尘仆仆专程赶来。更有甚者，因朋友推荐，还有人提前一天专程从香港赶来听这个讲座。<br />
<br />
除了胡一鸣本人写博客预告讲座活动，还有人专门为胡宣传。早在11月18日，就有名为梁柏华的人士在他博客宣告：恩师胡一鸣于11月22日在北京大学作风水演讲――《从科学视觉解读风水》（编注：与胡一鸣的演讲题目有出入，视觉应为视角）。<br />
<br />
胡一鸣是谁？其博客的介绍是：国际建筑风水研究学会理事长、北京大学易学社专家顾问。同时博客备注文字写道，“网络上说：胡一鸣老师是许多命理学家公认为有史以来最伟大的命理专家，最精准的台湾八字命理大师。”<br />
<br />
网上同时也有人称，胡“根本就是一个穷光蛋，其实是在台湾混不下去才来大陆的”。<br />
<br />
但胡一鸣还是站在了北大的讲台上，精神焕发，操着浓重的台湾腔。不过这位风水先生没有算到北京的天气，带着一点感冒来到讲堂。<br />
<br />
“胡说”风水<br />
<br />
“一命二运三风水，四积功德五读书。”这句话被胡一鸣用来谆谆教诲北大学子，“读书被放在最后一位，是最没用的。”这是胡的开场白。<br />
<br />
如同很多“大师”一样，胡一鸣把比尔・盖茨辍学创业、高中毕业即成为亿万富翁的案例搬了出来，以证明其读书无用论。<br />
<br />
“你们（北大人）辛辛苦苦读书几十年为了是什么？我想部分人可能有很崇高的理想，但绝大部分人会说就业找到更好的工作，说穿了，是要有更好的经济收入。”胡一鸣说，“假如有一门学科，花20个钟头，就可以掌握你的一生，你一辈子想要什么就有什么，那这个学科值得学不值得学？这叫风水学，又叫阴阳学。”<br />
<br />
“识得阴阳两路行，富贵达京城。”胡一鸣喜欢引经据典。他坚信懂得风水，就会所向无敌。胡告诉大家，他的风水学问可以帮人解决婚姻、收入、健康等所有问题。“我可以给你们保证，保证这些统统行。”<br />
<br />
胡一鸣也承认风水存在争议，因为很多人批评风水师大都是马后炮。胡也称“我所见到的风水大师也都是马后炮”，不过他自信满满地说：“我们不需要马后炮，我们预测的例子多不胜数。”“哪怕是马后炮，我也能让你心服口服。”<br />
<br />
“太极生两仪，两仪生四象，四象生八卦，八卦推演出六十四卦”，胡一鸣将这里的“生”作为“生育”来看待，两仪生育了四象，四象生育了八卦，于是“两仪是六十四卦的曾祖父”。<br />
<br />
胡一鸣提到，在武汉的一次风水大会上，一位某大学的博导、全中国数一数二的风水大师认为，学好风水，一定要先学好《易经》。这让胡一鸣纳闷不已：“我从来就不懂《易经》，但是我自以为风水要比他们好很多很多。”<br />
<br />
“他们根本不知道，风水是《易经》的曾祖父。”胡说。<br />
<br />
“简单的物理学解释”<br />
<br />
如其介绍所言，胡一鸣最引以自豪的是他所创的“阴阳法”风水学说，他试图将风水完全科学化，对风水从物理学中寻找原因。<br />
<br />
果不其然，不论在胡一鸣当天的报告中，还是在其博客上，从SARS的原因到甲型H1N1流感疫情的发展，从“害羞宅男易死于心脏疾病”到台湾首富郭台铭的家庭命运，从中国国家队跳水事业到央视大楼着火，胡的理论差不多可以解释这个星球上的所有人事物理。<br />
<br />
因为，其他风水师只顾着钻研《易经》，研习八卦，而胡一鸣是“用理论来推的”。“我是很单纯的小男孩，我任何事都从简单的角度思考。”胡一鸣煽情地阐释了自己的定位与风格。为了佐证他的单纯，胡称其风水理论以初中一年级的物理水准即可得到完全阐释，“用自然界的简单道理就想清楚了。”<br />
<br />
在两个多小时的讲座中，他例举了若干案例来证明其路冲风水理论，即依据钻木取火原理，两条路相交就相当于摩擦，就容易起火，而不是其他风水“大师”所言及的“三岔路冲说”。<br />
<br />
“真理只掌握在少数人手里。”胡一鸣告诉北大学子，现在的教育是错误的，现在崇尚西方，完全被西方牵着鼻子走，西方科学非常先进是完全错误的。“中国的科学是最先进的，西方毫无科学可言。”胡一鸣进一步解释说，因为西方是科技很发达，但他们没有科学，他们讲的是日新月异，他们一直推翻已有的错误的结论才取得进步。“他们不断在推翻，他们天天都是错的嘛。”<br />
<br />
胡一鸣宣称，中国的科学是发明以后就不用再有任何的变动，像中医、风水。“所有的癌症在风水师来讲，（解决）也是易如反掌。”<br />
<br />
“天上的星宿多聚于此”<br />
<br />
胡一鸣的讲座似乎很是引人入胜。听众王翊臣此后在其博客中提到，他提前两个小时就来占座了，在两个小时的讲座中，他没觉得饥渴，临走时两瓶水还是满的。<br />
<br />
尽管胡一鸣例举了诸多案例，但涉及原理太少了。这让包括王翊臣在内的许多听众很是不满。如王翊臣所言，他讲得津津有味，大伙听得迷迷糊糊。<br />
<br />
让经常来北大听诸如《道德经》讲座的王翊臣疑惑的是，胡一鸣预测到了央视的大火，为什么没有提前通知有关部门进行防范。不过他很快就想通了：这本身就是个神话，只可意会不可言传。天上的星宿多聚于此，听不明白是道行不深。王翊臣这样安慰自己。<br />
<br />
演讲末了的提问环节，有些混乱，让很多人失望。声音甜软的主持人发挥了东道主优势，一句“因为时间有限，今天的讲座告一段落”让很多人的提问戛然而止。<br />
<br />
讲台上还有一堆待解答的纸条，记者看到，在一张便笺纸上写着：“胡老师您好！看一处宅子，您会从哪些角度去看，为什么？”<br />
<br />
还有一张是在数学作业纸上写就的：“老师：您好，请问2012年12月的玛雅预言会实现吗？”听众恳切的态度可见一斑，不过胡一鸣没有时间去看纸条了。听众已经如流水般汇聚到了胡的周围。<br />
<br />
人群中，有人问胡：帮北大看看风水吧？<br />
<br />
“这要看北大各门的朝向，还要看学校最高领导人的办公室位置。”胡一鸣说。<br />
<br />
“风水”在高校复活<br />
<br />
北大未名BBS说，一个真正意义上的北大人，首先必须是一个社团人。在北京大学，就有一个社团在继承着老祖宗的易经学这份遗产，研习这份遗产也成了一部分北大学子的生活。<br />
<br />
这个社团就是易学社。<br />
<br />
不仅如此，北大还曾有授一门风水课。北大校团委负责学生社团的一位工作人员向《科学新闻》强调，这才是对前校长蔡元培先生“兼容并包”理念的响应，同时这位自北大环境学院毕业、城市规划设计专业出身的工作人员坚称，风水课的开设无可非议。<br />
<br />
这位年轻的工作人员提到的是于希贤的风水课。早在1985年，在当时地理系主任侯仁之的强力支持下，北京大学正式开了风水课，由获得硕士学位的于希贤独立教授。若非如此背景，诸如台湾胡一鸣的讲座不可能在北大进行。<br />
<br />
这位工作人员提到的“兼容并包”是指当年蔡元培在《〈北京大学月刊〉发刊词》写的：“大学者，‘囊括大典，网罗众家’之学府也。”当然，蔡元培自己并没有将中国的风水学囊括进北大来。科普作家陶世龙说：“蔡元培的兼容并包也不会让风水开课，风水是新文化运动所反对的具体对象之一。”陶世龙在接受《科学新闻》采访时还表示，“北大如今还不至于开风水课”，然而，出乎陶世龙意料的是，胡一鸣的讲座在北大校园风行如是。<br />
<br />
于希贤已在北大退休多年，但风水却在21世纪的北大“复活”了。<br />
<br />
在坚决反对风水的陶世龙记忆里，他和于希贤、侯仁之还曾经有合作，并认为当时于给他的印象还不错。据称，当年于希贤的职称评比未通过，陶世龙还为之鸣不平。<br />
<br />
两人关系慢慢随着于希贤的风水说发生了转变。陶世龙还曾告诫于，莫要陷进去。“但他还是离开了文化地理的研究。”陶世龙说。他曾批评于希贤是偷换概念，将风水换成风和水，也就是环境。“只要看一看风水的出处《葬经》就明白，没有了迷信也就没有中国的风水。”<br />
<br />
不过，2007年6月14日，在由文化部团委主办的“城市文化建设与文化遗产保护”讲座上，文化部某官员公开承认中国风水堪舆在建筑和城镇村落的规划设计上有一定价值。<br />
<br />
百度搜索发现，于希贤最近的新闻是，2009年9月13日，在成都娇子音乐厅主办的“北京大学国学易经与建筑风水（成都）论坛”上，从事风水学研究几十年的于希贤将风水与建筑学、城市规划学、景观环境学的辩证关系“娓娓道来”，“演绎得淋漓尽致”。网络媒体以“北大教授揭秘建筑‘风水’，800听众前呼后拥”来报道该事件。<br />
<br />
据报道，这类风水课在高校开设的案例不止北大一家专有，不少高校“秉着‘百家争鸣’‘学术无禁地’的原则”，都曾开设过风水课或者称作“堪舆”（即民间俗称的“风水”）的课程。如北京师范大学的“易学与建筑”专业，学制三年。安徽大学社会学系亦有“堪舆学”课程。另外，还包括如中国人民大学、东南大学、南京师范大学、华东师范大学、中山大学等若干所高校。<br />
<br />
陶世龙提醒记者，这个名单漏掉了最早开建筑与风水课的天津大学。<br />
<br />
针对易学研究进入高校，北京大学哲学系主任赵敦华教授对《科学新闻》表示，“作文本研究就够了”。而北京大学景观设计学研究院院长俞孔坚则以“相信没有上帝的人可以是科学家，相信需要上帝的人也同样可以是科学家”来回应对风水的指责。<br />
<br />
资料<br />
<br />
传媒对“风水”的推崇<br />
<br />
不仅仅是高校，新闻媒体同样存在为风水学鼓与呼的声音。某媒体曾用“‘和而不同’多元文化时代的到来”来肯定风水热。<br />
<br />
中国科学院地理所副研究员、《中国国家地理》杂志社执行总编单之蔷以“风水：中国人内心深处的秘密”做了一期《中国国家地理》风水专辑。这引来不少争议。陶世龙指出，该专辑“不顾风水已有明确的定义和实际内容，将地理与风水混为一谈”。<br />
<br />
中央电视台也不甘寂寞，作为中国大众文化风向标的“百家讲坛”节目亦连续播出15集“易经的奥秘”节目，该节目由台湾师范大学曾仕强教授主讲。央视网上这样介绍：揭开算命神秘的面纱，无论是风水还是命运，都是一种客观规律，而《易经》所揭示的，正是宇宙人生所潜在的这种共同的客观规律。那么如果我们了解并掌握了这种规律，是不是就可以在有限的条件下，把自己的命运掌握在自己手里了呢？<br />
<br />
<br />
媒体热捧风水，公众亦是热情高涨。据网易科技的一项调查“你如何看待如今的风水”，有4862次投票。这项于2006年1月18日至2007年1月25日进行的投票结果是：“封建迷信，坚决打击”为162票，仅占3.33%；“风水体现的是一种和谐”为2672票，比例高达54.96%；“民族传统文化，要保护”为1707票，占35.11%。<br />
<br />
(本文来源：科学新闻 作者：孙滔)</div>

							
							
							
							
