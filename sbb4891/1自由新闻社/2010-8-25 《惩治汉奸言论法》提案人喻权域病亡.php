【旗帜简讯：萧萧报导】我国哲学社会科学界知名学者，坚定的马克思主义者，忠诚的中国共产党党员和杰出的共产主义战士，中国人民政治协商会议第九、十届全国委员会委员，中国社会科学院学术委员会委员，中国社会科学院研究生院教授，中华人民共和国国史学会副会长，中国人权研究会副会长，中共中央对外宣传办公室秘书长，中国社会科学院新闻研究所所长，原《真理的追求》主编喻权域同志的遗体告别仪式于18日在北京八宝山革命公墓举行。<br />
<br />
北京八宝山革命公墓兰厅室内庄严肃穆，哀乐低回。正厅上方悬挂着“沉痛悼念喻权域同志”的横幅。喻权域同志安卧在鲜花翠柏丛中，遗体上覆盖着中国共产党党旗。告别室内四周摆放着全国政协、中国社会科学院、原《真理的追求》杂志、原《中流》杂志、毛泽东旗帜网等单位和个人送的花圈。<br />
<br />
告别仪式在低沉的哀乐声中开始，喻权域同志的夫人带领子女及亲朋好友们伫立在喻权域同志遗体旁。前往的群众在遗体前肃立默哀，向喻权域遗体三鞠躬并与亲属一一握手，表示沉痛哀悼和深切慰问。在这样哀伤的情景中，人们强忍着泪水与喻权域同志的遗体作最后的告别。<br />
<br />
97岁高龄的马宾同志也来到了告别仪式现场。网友们纷纷前往吊唁，还有从外地特地赶来的同志。喻权域同志亲属及生前友好、家乡代表和首都各界朋友等也前往送别。<br />
<br />
喻权域同志一生谦虚谨慎，淡泊名利，著述丰硕，直至晚年仍笔耕不辍。他为追求真理，宣传党的方针政策，贡献了自己毕生的力量。他的一生是革命的一生、战斗的一生、光辉的一生，是为党和人民无私奉献的一生，无愧于坚定的马克思主义者和杰出的共产主义战士。他的革命精神、高尚品德和崇高风范永远值得我们学习和怀念。<br />
<br />
