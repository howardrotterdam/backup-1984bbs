<br />
原《真理的追求》主编之一喻权域同志于2010年8月14日8：30在家中不幸逝世，享年75岁。<br />
<br />
<img src="http://honglin.org/UploadFile/2007-3/200731011504937730.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
喻权域，1935年出生于四川省荣昌县（今属重庆市）。1950年3月高中肄业，多报三岁参加革命干部队伍。先在新繁县桥乡剿匪、组织农协会、搞减租退押，然后在新繁县政府、县委工作，参与创办四川第一个农业生产合作社――新民社。1954年发表通讯《快乐的女社员》，并由四川人民出版社出版同名报告文学集。同年加入中国共产党，调成都任新华社四川分社记者。<br />
<br />
1979 年12月任《半月谈》杂志主编；1982年后，历任新华社国内部副主任、新华社《经济参考报》总编辑等职。1989年9月调任《人民日报》编委兼总编室主任。1993年9月调中共中央对外宣传办公室任秘书长，组建中国人权研究会，任副会长，参与国际人权合作与人权舆论斗争。1996年9月调中国社会科学院专职担任（以前是兼任）新闻与传播研究所所长。1998年后任中国社会科学院学术委员会委员，全国政协第九届、第十届委员，全国政协外事委员会委员。<br />
<br />
喻权域同志在长期的新闻工作中，撰写了大量的通讯、研究报告和学术论文，并多次获奖，其中专著《中国人权问题纵横谈》获中宣部“五个一工程”奖，专著《论三个时间差―与两代人谈社会主义》获文化部等四部委颁发的“优秀图书”奖，又获吴玉章马克思主义理论学科优秀奖，论文《世妇会报道对新闻学传播学的启示》获第七届中国新闻奖新闻论文一等奖。<br />
<br />
从1984年起，担任中国社科院研究生院硕士生导师、教授。享受政府特殊津贴。<br />
<br />
2007年中华人民共和国人大代表喻权域在十届全国人大第五次会议中提出《惩治汉奸言论法》，受到人民群众的热烈拥护。<br />
