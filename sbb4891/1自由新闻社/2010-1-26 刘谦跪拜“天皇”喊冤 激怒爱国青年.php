<a href="http://www.huanqiu.com" target="_blank">http://www.huanqiu.com</a> 来源：四川在线-天府早报 网友评论条 进入论坛 2010-01-26 08:56<br />
 刘谦的“下跪”<br />
<img src="http://himg2.huanqiu.com/attachment/100126/53b4b08ee9.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
视频截图<br />
<img src="http://himg2.huanqiu.com/attachment/100126/fa5d155c2a.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
 　　因为一系列在日本做节目的视频，春晚大红人刘谦再次成为焦点，不过这回，招来的全是骂名。身穿中国传统绸缎对襟衫的刘谦，在节目中向“日本天皇”毕恭毕敬下跪，更自称从小就崇拜天皇，是天皇的粉丝！此番言行一经曝光，激怒了大批网友，讨伐的阵势不输当年杨丞琳的“八年抗战脑残门”。<br />
<br />
跪拜“殿下”刘谦自称是粉丝？<br />
<br />
　　从网友曝光的系列视频中，可以清楚辨认出刘谦。据说他上的是日本2010新年节目，画面上是一间典型的日式厢房，跪着7名日本伺女和几名身着绿色日本和服的男士，正中坐着的，是一位身着橙色日本服，脸上被刻意涂白的男人，刘谦称呼他为:“殿下。”<br />
<br />
　　刘谦当日的服装具有很鲜明的中国元素，蓝色的绸缎对襟衫，身上有圆圈式的刺绣。他一进门，就向殿下问好，在这位殿下说完“欢迎”之后，刘谦双膝着地，端正地跪在殿下的正对面。<br />
<br />
　　视频截图上显示有这样的对话:“我在台湾很知名度的，我小时候就是您的粉丝了。”说这话时，刘谦还特别垂下了头。这时，那位白脸的殿下转身从旁边拿起一样东西，说:“作为新年礼物，送点东西给你吧。这个没问题吧，DVD，给你了。”刘谦的反应有些夸张，他反手指着自己，脸上有很惊讶、很错愕的表情，网友们愤怒地指责他“故意装成受宠若惊的汉奸相儿”！从画面上来看，礼物仿佛是一套日本的卡通碟，刘谦拿到手后，开心地对着镜头大笑，身后的日本人也随之鼓起了掌。<br />
<br />
　　网友炮轰建议春晚封杀刘谦<br />
<br />
　　视频曝光后，有网友分析，这可能是一期模仿秀节目，“那个涂白脸的，演的是日本天皇。”大家对刘谦参与这样一期日本新年节目，非常愤怒，“居然说自己从小就崇拜天皇，还是天皇的粉丝！刘谦，你知不知道这样的说法极大伤害了13亿中国人民的感情！”“刘谦，对你太失望了！再也不看你的表演了！”<br />
<br />
<br />
　　值得一提的是，备受瞩目的虎年春晚早已向刘谦发出了邀约，如不出意外，刘谦又将在年三十与全国观众见面，而昨日该视频曝光后，网友叫其从春晚“下台”的声音颇为强烈，“建议大家封杀刘谦，春晚是咱中国最传统最喜庆的节目，不想看到这个给日本天皇下跪的人！伤感情了！”<br />
<br />
<img src="http://himg2.huanqiu.com/attachment/100126/2e774f6a89.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
经纪人喊冤:事情是真翻译有误<br />
<br />
　　随后，记者致电刘谦的经纪人何晃杰，听完记者对事件的叙述后，何先生连连表示:“我先看看，再跟你讲好吗？”他表示自己还不清楚网上的状况，需要先亲自上网浏览。记者追问刘谦到底有没有上过这样一期节目，何先生并未否定，“不好意思，能不能我先看看，了解一下，呆会儿再跟你讲。”<br />
<br />
　　半小时后，记者再次致电何先生，他承认刘谦的确在日本做过这样一期节目:“我有一份声明会发给你，你们看了声明再作报道好吗？”<br />
<br />
　　下午6点半，何先生发了一份声明到记者的邮箱，他表示网上曝光的视频节目是刘谦去年12月受邀到日本富士电视台录制的《笨蛋城主》新年特别节目，那个被网友视为“日本天皇”的其实是日本的志村健先生，“内地的朋友可能不太知道谁是志村健，但是在中国台湾地区以及日本，志村健的名号可说是无人不知无人不晓，纵横演艺界30多年，演艺地位至高无上，犹如中国的‘赵本山’。是位德高望重的表演艺术家。说起他所创造的经典角色‘怪叔叔’，经典到简直可以写进日本历史教科书。”<br />
<br />
　　至于刘谦下跪，称其“殿下”，何先生在声明中表示那完全是翻译有误，“在网络上转载的节目内容中，因翻译有所偏差，而造成大众的误解，其实在节目中皆是以‘城主’相称。”<br />
