麦肯锡公司猜测2009年北京朝阳区有一幢大楼倒塌，可能会是哪幢呢？ <br />
 <br />
<br />
 作者：北大关机男 提交日期：2008-11-9 21:28:00 访问：24996 回复：514&nbsp;&nbsp;<br />
<br />
??（信息来源：今天的参考消息）<br />
　　全球著名的咨询公司――美国麦肯锡公司预测了2009年中国可能会发生的一些重要事件，其中有一件就是在这一年里，北京朝阳区的某一栋大楼将会倒塌，从而引起中国城市建筑规划发生重要调整。<br />
　　具体是哪一栋楼比较危险，该公司没有指明，可是中国这么大、北京楼这么多，它却偏偏指出这事可能会发生在北京朝阳区。<br />
　　麦肯锡公司没有点名，是因为它不敢得罪人，再说了，万一该楼09年没倒，偏偏10年、11年或者再等几年才倒，那就是预测不准，岂不是被人骂死？<br />
　　这几年中国，北京也好、上海也好、广州也好，标新立异的建筑实在太多了，能进入世界建筑奇迹的不止这么一栋楼。有的要建世界最高的建筑，有的要让你眼花缭乱，有的要挑战地心引力......<br />
　　比如吧，朝阳区东三环与光华路交界处就有这么一栋大楼，勇于挑战传统物理学理论。当初有领导一拍屁股：“建！”于是花巨资请来了洋人设计师，于是这栋大楼就成了洋人设计师的“淘金地”和“试验田”。<br />
　　这栋大楼的预算起初是50亿人民币，建着建着有关方面就心虚了：北京可是地震高发区啊！这样的超长悬挑，严重扭转不规则，整体结构倾覆力矩超大导致基础设计难度极大，不管怎么“论证”也不可能比中规中矩的造型更安全、更稳定、更可靠。<br />
　　许多网友评论道：造型不好，像小孩的开裆裤。连儿童都知道，搭积木搭成这种形状会造成什么后果。这楼倒了自己也无所谓，砸了无辜的行人，那可冤大了。芙蓉姐姐把自己的肥腰可以扭成S型，可是一栋摩天大楼也这样搞，还真是令人心惊胆战。<br />
　　北京好久不见大地震了，但小地震每年还是都有的。于是有关方面追加了预算，不算内部装修现在也超过100亿了。100亿是个什么概念？超过2007年西藏和宁夏两区的全年财政收入之和，相当于2007年海南省的财政收入，相当于全体中国人每人拿出8块钱。反正中国人钱多的是！<br />
　　100亿可以建多少所希望小学，可以扶助多少贫困大学生，可以修多少里程高速路，可以解决多少下岗职工的生活困难？还可以去提高中国国防力量，可以去改善人民生活质量就跟不用说了……<br />
　　偏偏住在这栋大楼里的人也不争气，新闻不断：说灾区的人像过年、当众打哈欠、打饱嗝、夫妻吵架揭绯闻、穿着大裤衩给全国人民看......<br />
　　听说在这栋大楼里工作的人收入颇高，当然，想进那里工作，不是一般人能做到的。<br />
　　当然，麦肯锡所说的危楼也未必就是这栋楼，同样悬的楼多着呢！</div>

							
							
							
							
