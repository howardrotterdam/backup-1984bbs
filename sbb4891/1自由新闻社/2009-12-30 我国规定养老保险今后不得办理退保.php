													<h2>我国规定养老保险今后不得办理退保</h2>
						
						
																											<div id="postmessage_404594" class="t_msgfont"><a href="http://news.qq.com/a/20091230/000816.htm?qq=0&amp;ADUIN=23374338&amp;ADSESSION=1262132287&amp;ADTAG=CLIENT.QQ.2587_.0" target="_blank">http://news.qq.com/a/20091230/00 ... G=CLIENT.QQ.2587_.0</a><br />
<br />
<img src="http://img1.gtimg.com/news/pics/26472/26472780.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
<br />
昨日，国务院办公厅转发了人力资源社会保障部、财政部关于《城镇企业职工基本养老保险关系转移接续暂行办法》，办法明确了单位缴费按照缴费基数12%转移，共计45个工作日办妥转移手续，同时规定参保人不得退保。至此，重庆的养老保险在制度上也实现了省内省外无障碍转移对接。<br />
<br />
解读1 办理要花多久？<br />
<br />
45个工作日办完<br />
<br />
对于参保人员跨省流动就业的，转移养老保险关系需要走三个流程，新参保地审核转移接续申请并向原参保地发出同意接受函――原参保地办理转移手续――新参保地接受转移手续和资金，三个流程走完之后即可办妥转移接续手续，政策规定每个流程最多15个工作日，也就是说对于参保者来说，最多45个工作日就可以将全部手续办完。<br />
<br />
有关负责人表示，转移手续极大地方便了参保人，参保人只要申请即可，剩下的工作将由两地社保部门进行对接转移。<br />
<br />
办理流程<br />
<br />
1参保人员在新就业地按规定建立基本养老保险关系和缴费后，由用人单位或参保人员向新参保地社保经办机构提出基本养老保险关系转移接续的书面申请。<br />
<br />
2新参保地社保经办机构在15个工作日内，审核转移接续申请，对符合本办法规定条件的，向参保人员原基本养老保险关系所在地的社保经办机构发出同意接收函，并提供相关信息；对不符合转移接续条件的，向申请单位或参保人员作出书面说明。<br />
<br />
3原基本养老保险关系所在地社保经办机构在接到同意接收函的15个工作日内，办理好转移接续的各项手续。<br />
<br />
4新参保地经办机构在收到参保人员原基本养老保险关系所在地社保经办机构转移的基本养老保险关系和资金后，应在15个工作日内办结有关手续，并将确认情况及时通知用人单位或参保人员。<br />
<br />
解读2 资金如何转移？<br />
<br />
企业缴费转走12%<br />
<br />
《暂行办法》明确，资金转移中，个人账户储存额全部转移。而统筹基金(即单位缴费)实行部分转移，即以本人1998年1月1日后各年度实际缴费工资为基数，按12%的总和转移，参保缴费不足1年的，按实际缴费月数计算转移。<br />
<br />
解读3 是否能够退保？<br />
<br />
今后参保不得退<br />
<br />
在昨日的转移接续办法中有一个信息非常重要，不得退保，它意味着，目前普遍存在农民工的“退保”现象有可能一去不复返。通知这样规定：未达到待遇领取年龄前，不得终止基本养老保险关系并办理退保手续。<br />
<br />
那么，没有达到领取条件的怎么办，会不会其缴纳的养老金就烂在账户里取不出来了？不用担心，全国人大常委会日前对《社会保险法(草案)》进行了三审，其中规定，养老保险缴费不足15年者，可延续缴费并享受养老保险待遇。也就是说达到退休年龄，但同时又没有满足领取养老金条件的，可以通过延长缴费的方式，达到领取养老金条件。从中可以判断的一个信号是，国家希望今后每一个人都能够通过各种方式领到养老金，真正达到“老有所养”。<br />
<br />
解读4 农民工是否有选择？<br />
<br />
可权衡加入哪个保险<br />
<br />
昨日，人力资源与社会保障部养老保险司副司长尹志远说，目前很多农民工退休后都可能会选择返乡，只要符合条件，农民工可以自由选择享受城镇养老保险还是新农保。<br />
<br />
对于新农保待遇要低于城保的疑问，尹志远认为，农民工可以自己权衡究竟哪一个更加有利。新农保目前在试点当中，未来还有完善的空间，对农民来说，领取上也更加便利。总之，要给农民工自由选择的权利。尹志远介绍，未来农民工在城镇参加企业职工基本养老保险与在农村参加新型农村社会养老保险的衔接政策，还在讨论之中。(据广州日报)<br />
<br />
退休人员可在参保满10年地区领养老金<br />
<br />
如果在最后参保地参保满10年，则在最后参保地领取待遇；如在最后参保地参保不满10年，依次向前推至满10年的参保地办理待遇领取手续；各地参保都不满10年，则在户籍所在地办理待遇领取手续。[详细]<br />
