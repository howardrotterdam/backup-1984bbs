													<h2>香港举行政改电视辩论 建制派人士突纷纷改变立场</h2>
						
						
																											<div id="postmessage_686558" class="t_msgfont">特首曾荫权与公民党党魁余若薇明日举行政改电视辩论，昨日由双方代表胡定旭及张达明，以及主持吴明林负责公众提问抽签，今次港台共收到2876条公众问题，当中向曾，余提问的比例是二比一。吴明林表示，他们抽出所需的六条问题，只有两条因为字数远超规定的120字被筛走，三人小组承诺保密问题内容，不会知会辩论双方。香港记者协会及外国记者会发表声明，批评政府拒绝让传媒采访余曾辩论，认为若政府有诚意透过传媒作出全面报道，政府必须让传媒在现场见证辩论过程。<br />
---------------------------------------------------------------------------------------------------------<br />
<br />
【明报专讯】距离政改表决尚有一星期，中央是否接纳泛民的“普选区议会代表”方案成为焦点。虽然中联办文宣部长郝铁川以强硬措辞否定方案，但行会成员张炳良今日在本报撰文力撑普选联方案，认为是否违反人大决定其实有“灰色地带”，在技术层面上不算是“直选”或普选，他不点名希望中央和特区接纳民主党方案，为特区管治带来新契机，新气象。 <br />
<br />
<br />
另外，政府昨推出第三轮“起锚”宣传广告，张炳良和行会成员梁智鸿，夏佳理和胡红玉粉墨登场。张炳良在广告中继续为政府推销方案，他说：“浪费5年又5年，香港可以有多少个10年？... ...政制原地踏步，香港是大输家，寻找突破才是出路。“ <br />
<br />
<br />
指有否违人大决定属灰色地带 <br />
<br />
<br />
继早前撰文呼吁泛民“以时间换取空间”，张炳良再在本报撰写文章“改良方案值得考虑”（详刊A22座观点版），力撑民主党和普选联提出的区议会方案。他在文中指出，方案涉及两大问题，其一是与07年人大常委决议是否相勾ィ张炳良认为这只是“灰色地带”，因为政府早已定义区议会属于功能组别，由市民投票选出区议会功能组别议员，技术上非：“分区直选”，亦非完全属普选。 <br />
<br />
<br />
第二个问题是方案能否透过本地立法处理。张炳良认为答案是正面，政府可在本地立法范围，界定选民资格及投票方式。 <br />
<br />
<br />
张炳良强调，如果这个方案成为2012年政改能否通过的关键，“为何不可以用『求大同，存大异』的精神寻求妥协，使进一步海阔天空，为整个特区管治带来峰回路转的新契机，新气象？“他续说：”若只是故步自封，原地思维，自我捆绑，则永远走不出香港政治的恶性循环！“ <br />
<br />
<br />
张文光：证明方案可被接受 <br />
<br />
<br />
民主党张文光回应指出，作为建制派的张炳良也可以接受方案，证明方案的确可被接受。他强调民主党只是以这个方案作为取消功能组别的手段，至于方案能否成事，还要看是否获中央“祝福”，现阶段仍不敢乐观。 <br />
-----------------------------------------------------------------------------------<br />
<br />
胡汉清转：方案没违人大决定 <br />
<br />
【明报专讯】除了张炳良，曾批评民主党方案不可接受的全国政协委员兼“基本法”研究中心主席胡汉清，亦一改口风，日前接受传媒访问时，指区议会界别就算交由市民投票，由于候选人只可以是区议员，已是兼顾了均衡参与，亦大大增加民主成分，符合07人大决定及“基本法”原则。这跟胡汉清上周五出席政协讲座时，直指方案违反人大决定的口风截然不同。 <br />
<br />
<br />
指大大增加民主成分 <br />
<br />
<br />
对胡汉清的“突然转”，加上张炳良力撑泛民方案，有政坛资深人士分析，他们有可能是看到政改有转机，普选联方案有可能得中央“祝福”，因此纷纷站出来表态，务求在方案最终成事时，可以挂上“成功争取”的横额。 <br />
<br />
<br />
“胡汉清向来是无宝不落，亦紧贴中央路线，所以他突然转，亦不能小觑。可能他是收到风知道有转机，才会做变脸人。” <br />
<br />
<br />
消息人士亦称，虽然中联办以强硬措辞对待泛民方案，事实上方案仍在中央的“台面上”，未正式“说不”，仍然有一线生机。 <br />
<br />
<br />
由于今天开始是内地假期，相信要待明天“曾余世纪大辩论”结束后，才是中央还价的关键时刻。<br />
