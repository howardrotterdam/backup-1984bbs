													<h2>百度须在首页向“海运女”连续道歉三天</h2>
						
						
																											<div id="postmessage_714941" class="t_msgfont">因搜索引擎内存在大量涉及殷小姐裸体照片、生活照片和单独反映女性人体部分隐私器官的照片，著名网站北京百度网讯科技有限公司（以下简称百度公司） 被上海的殷小姐告上法院。今天下午，上海市静安区人民法院一审判决百度公司停止对殷小姐名誉权的侵害，断开搜索引擎中可辨认殷小姐相貌的涉案图片链接；删除网站上保存殷 小姐的个人信息；连续3天在百度首页醒目位置刊登向殷小姐道歉声明，消除影响为殷小姐恢复名誉；支付殷小姐经济损失人民币2000元、精神损害抚慰金人民 币两万元。<br />
<br />
<br />
　　2009年5月13日，殷小姐向警方报案，称有大量涉及她身体隐秘部位的裸体照片被他人上传至网站www．kdsphome．net中。该事件 被冠以“‘海运女’艳照门事件”，继而被多家媒体报道。<br />
<br />
　　同年5月18日，殷小姐委托律师在《时代报》上发表公开声明，称“海运门不雅照片事件”为故意侮辱女性的恶性事件，要求各搜索门户、论坛、博客 等网站尽快采取措施，删除任何侵犯受害人“YH小姐”合法权利的相关文字内容、照片、链接以及不实报道。<br />
<br />
　　同年5月25日，经上海公证处公证，殷小姐在百度搜索引擎中输入“海运女殷?艳照”进行搜索，发现大量涉及同一女子的裸体照片、生活照和单独反 映人体部分隐私器官的照片。<br />
<br />
　　同日，在www．baidu．com网站的另一栏目“百度百科”内，有以“海运女”命名的词条。打开该词条，在“海运女――个人资料”项下，可 见“姓名：殷?；性别：女”等资料，在其他部分还有对此事件的详细描述，其中使用了“赤裸”、“不堪入目”等词汇形容照片，并网络链接 “http：／／www．redianjh．cn／show．php？tid＝372”，文字说明该链接为“上海海运学院海运女艳照门最全照片合集”。<br />
<br />
　　2009年7月13日，殷小姐诉称与案外人朱某系恋人关系，2009年4月，双方因感情不和分手。朱某出于报复于2009年5月8日,将大量涉 及她隐私的图片上传至互联网，并遭到网友的搜索。自己曾于5月13日委托律师登报声明，要求各网站采取措施删除相关内容。<br />
<br />
　　但直至2009年6月2日，以百度为代表的搜索网站对上述图片及私人信息未进行任何处理，导致原告的照片及个人信息在网络上广泛传播。2009 年10月25日，在百度网站开设的“百度百科”栏目下的“海运女”词条内，仍发现有自己的姓名、性别、毕业院校名称等个人信息，甚至还提供相关照片的下载 链接，殷小姐认为百度该行为严重侵犯其合法权益遂起诉法院。<br />
<br />
　　百度公司辩称，殷小姐登报声明不构成有效通知，理由是《时代报》系地方性报纸，不符合有关法律规定。涉诉照片是发布并受控于第三方网站，百度作 为搜索引擎服务提供商，只是根据用户输入的关键词，自动生成与第三方网站的链接，搜索引擎所起的是检索作用，本身没有刊登、发布、传播涉诉照片。鉴于网站 计算机对图片及音频不能全部识别，搜索过滤中存在误差在所难免。为避免搜索结果出现侵犯殷小姐“不雅照”，百度投入了近200人对搜索结果进行过滤和删 除，主观上不存在过错。但第三方网站仍然不断上传新的图片，难免在搜索中仍然存在殷小姐部分图片。某些图片系殷小姐生活照，不涉及黄、赌、毒等违反强行性 法律、法规的内容，百度没有义务主动作处理。百度称在殷小姐投诉前，已对相关内容进行了断链。开庭后，根据殷小姐投诉，又履行了监管义务，因此不同意殷小 姐的诉讼请求。<br />
<br />
　　法院审理后认为，对不特定人主张权利的登报声明，应包括具有适格的主体，否则任何人均可匿名登报声明。殷小姐委托律师刊登公开声明，并未明确表 示权利人主体，仅以“YH小姐”代之，该登报声明不构成有效的通知。<br />
<br />
　　自2009年5月8日案外人上传殷小姐“不雅照”后，众多新闻媒体对此事进行了跟踪报道，多家报刊均在醒目位置刊登相关新闻，在较短时间内已形 成新闻媒体报道的热点，导致大量网友在寻找涉案“不雅照”并下载，而百度恰恰是提供搜索服务的网站。<br />
<br />
　　此外，在2009年5月15日匿名案外人的投诉中，亦提及用“殷?”进行搜索到的最佳答案“助长了散布不良信息和图片”，百度应不晚于2009 年5月15日知道，搜索引擎被网友用于搜索涉案不雅照片。殷小姐所诉侵权内容可分为三部分，即搜索结果中原告身体大面积裸露“不雅照”、生活照、“百度百 科”中个人信息及照片下载链接。<br />
<br />
　　法院认为，本案涉及面广，社会影响大，且殷小姐系年仅26岁的女子，在案发后因百度公司的过错，导致她承受精神痛苦和生活困扰，使得她身心遭受 了重大打击，法院支持殷小姐要求支付精神损失抚慰金的主张，考虑到百度并非直接发布侵权照片者，遂酌定为两万元。<br />
