<img src="http://images2.china.com/news/zh_cn/social/1007/20100414/r_2010041408303215174900.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<a href="http://news.china.com/zh_cn/social/1007/20100414/15894695.html" target="_blank">http://news.china.com/zh_cn/social/1007/20100414/15894695.html</a><br />
昨日中午，广州大学城华南理工大学学术交流中心门口，将近20名专家、教授参加了一个“学术交流会”后，参与或目睹了560多本全新书籍的焚毁。活动组织者林先生称，被焚毁书籍都是盗版书、伪书或误导书，这些书是污染人心灵的垃圾，烧毁这些书是希望读者能拒绝这些心灵垃圾。<br />
<br />
&nbsp; &nbsp; 记者了解到，林先生曾策划“总裁抱抱团”、“老板宣誓不欠薪”和“向雷锋像下跪”等备受争议活动。昨日，林先生再做惊人举动，邀请将近20名专家、教授在华南理工大学召开“心灵富豪学术交流会”后，于中午12时在华南理工大学学术交流中心门口开始焚烧书籍。<br />
<br />
&nbsp; &nbsp; 林先生首先从工作人员抬来的三四箱书籍中捡出郭敬明著的《梦里花落知多少》，一边用力撕碎，一边把碎纸片扔进身前一个火炉中。旁边的十多名专家、教授见状，也加入烧书行列。<br />
<br />
&nbsp; &nbsp; 记者注意到，被烧的书中有《羊皮卷》、《用人厚黑学》、《生男生女自己定》等曾畅销一时的书籍。<br />
<br />
&nbsp; &nbsp; 《梦里花落知多少》为何成为被焚烧书之列？林先生说：它(指《梦里花落知多少》)被法院公开裁决有大量抄袭内容，作者为了谋利不择手段，是拜金主义的体现，其作品当然是心灵垃圾，该烧。“抄袭者都可以成功，成为偶像；许多人都会跟着去抄袭，这是非常坏的榜样，”林先生还说。<br />
<br />
&nbsp; &nbsp; 林先生认为，垃圾书籍的危害比起地沟油、毒奶粉、假疫苗对人的危害更大；焚烧垃圾书是为了惊醒社会，让更多的人行动起来，向心灵垃圾宣战。<br />
<br />
&nbsp; &nbsp; 面面观<br />
<br />
&nbsp; &nbsp; “焚书表明了抵制垃圾的态度”<br />
<br />
&nbsp; &nbsp; “焚烧这些盗版书、侵权书，至少表明了我们抵制‘心灵垃圾’的态度。”现任广东营销学会副会长、《营销管理》杂志社总编辑的袁乐清是昨日加入到焚书行动中的学者之一，他希望通过这次焚书活动号召人们消除功利心，更多地关注自己的心灵，成为“心灵富豪”。袁乐清同时表示，盗版书打成纸浆循环利用，经济环保，拒绝“心灵垃圾”用撕书的方式更妥。<br />
<br />
&nbsp; &nbsp; “侵犯著作权的书应该烧掉”<br />
<br />
&nbsp; &nbsp; “焚书本身只是一种象征性的行为。”经济史学研究专家叶显恩说，侵犯他人著作权的书应该烧掉。<br />
<br />
&nbsp; &nbsp; “但如果仅仅是因为学术观点不同，就去抨击他人作品、烧他人书籍，这样做则不对。”叶显恩说，他没有加入到焚书活动中。<br />
<br />
&nbsp; &nbsp; “盗版书不一定都是垃圾”<br />
<br />
&nbsp; &nbsp; “盗版书不一定都是垃圾，有些盗版书内容还是好的，”广东省社科院专家江中孝对焚书持否定态度。<br />
<br />
&nbsp; &nbsp; 江中孝认为，书籍内容好坏的评判标准不能按某个人或某些人自行制定的标准来草率界定；社会需要多元文化，也应该允许不同的文化存在。</div>

							
							
							
							
