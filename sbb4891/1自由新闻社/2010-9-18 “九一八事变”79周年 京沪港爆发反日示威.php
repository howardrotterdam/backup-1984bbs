&nbsp; &nbsp; &nbsp; &nbsp; <br />
　　（综合讯）「九一八事变」79周年，中国网民发起反日示威。在北京的日本大使馆外，大批市民聚集抗议。大使馆的保安加强。<br />
<br />
　　星岛日报报道，一名来自安徽的男子，手持扬声器，身穿写有抗议字句的白色上衣，率先在日本驻北京大使馆附近抗议，并高叫口号，要求日本「滚出钓鱼台」和「中国万岁」。有示威者并一度脱去上衣，向在场采访的记者，展示身上的中国国旗。<br />
<br />
　　有示威者践踏日本国旗，指日本不尊重中国，亦有示威者自制有钓鱼台岛图案的蛋糕，插上中国国旗。<br />
<br />
　　其后陆续数十名示威者出现，高举标语抗议，大批公安在场维持秩序。<br />
<br />
　　在大使馆门外，大批公安在场戒备，保安较以往严密。在大使馆正门附近停放近20辆公安车辆，附近的日坛公园亦有大批便衣公安巡逻。另外，由长安街进入使馆区的所有道路都有公安车戒备。<br />
<br />
　　而在网民号召聚集出发示威的日坛公园，一早有大批公安车辆戒备。<br />
<br />
　　中国网民发起在全国各省市游行，抗议日本宣称钓鱼岛为领土部分，北京、上海、广州和深圳的网民纷纷响应。<br />
<br />
　　在上海，通往日本总领事馆的道路已封锁，大批民众拉起横额，要求释放被拘捕的中国渔船船长，并呼吁在场的围观者在横额上签名支持。<br />
<br />
　　在广州，逾百名公安及武警在日本驻广州总领事馆的花园酒店外戒备。<br />
<br />
　　在深圳，网民则在华强北路和日资百货公司抗议，戴上头盔、手持盾牌的公安人员在华强北路一带戒备。<br />
