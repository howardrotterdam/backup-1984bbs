　　然而，如果我们透过这场活动漂亮的宣传口号，我就会发现这是一场劳民伤财的环保秀。因为中国的电网不像新西兰等西方国家有大量的燃油、燃气机组，全民停电一小时期间关闭几台机组，确实能够起到一定的节能作用。我国的电网以煤电为主，机组的熄火和启动都需要很长的时间。一旦全国都停电一小时不仅不会节约能源，可能反而会增加正常的煤耗。如果控制不好还会引起电网事故。客观地说我们的广大市民在最需要用电的黑夜里，停止用电一个小时所得到的，决不是什么节约能源的实际效果，而是一场劳民伤财的为环保组织捧场的环保秀。节约用电最有效、最实际的办法，是每个人都随时随地的注意节约。而不是这样大家一起，作一场环保秀的游戏。<br />
<br />
　　现在的世界搞环保确实很时髦，环保秀也是很多组织和政客哗众取宠的重要方式。然而，如果我们大家不能从科学的态度出发，热衷于为自己捞取某种资本的环保秀，那么我们不仅不会保护环境，而且还会破坏环境。除了这可笑的“全球一小时”之外，我们还有很多环保活动都带有作秀的成分。<br />
<br />
　　例如，所谓的“无车日”。为了节能减排，动员大家尽量的少开汽车当然是好事。但是，某些环保组织为了作秀，非要大家一起搞一个无车日。这样虽然大家在那一天都尽量不开车了，但是，根据我个人的体会，所有那一天要办的事情，几乎都要想办法安排在无车日的前后两天。所以，无车日的前后两天必然是拥挤日。因此，如果从道路资源的合理利用角度来看，我们搞无车日的结果，不仅不能节约能源，反而一定会浪费能源、增加排放。<br />
<br />
　　好在我们的广大市民群众的脑子都不算糊涂，虽然环保组织“无车日”的口号喊得很响，但是北京的无车日，我看到路上的车子还是一点也不见少。因为他们都知道参加无车日的活动绝不是节能，而不过是帮助环保组织作秀。相反只有抓紧在环保组织搞的无车日出来办事，才是真正的节能减排。事实证明还是“群众是真正的英雄”。<br />
<br />
　　我相信这个“全球一小时”也将验证“群众是真正的英雄”这一真理。因为，我们真正的节能减排的办法，是要在平时的每时每刻注意节约能源，而绝不是热衷于和一些环保狂热组织一起作秀。</div>

							
							
							
							
