<br />
今天是世界艾滋病日，中国红十字会在北京南站举行防治艾滋病倡导活动，然而在现场官式亮丽的仪式背后，却有20几位艾滋病患者家属站出来，倾诉他们悲苦的遭遇，他们不是官方活动的成员，而是有冤无处诉的受害者。<br />
<br />
不愿透露姓名的一位母亲哭泣着说，她的儿子六个月大的时候发现得了血友病，使用了上海生物制品研究所生产的VIII因子，感染了艾滋病和C型肝炎病毒，至今十多年，饱受折磨，儿子因长期服用病毒药物，如今全身关节肿大，疼痛难捱，无法行动，庞大医药费让她们生活难以为继。<br />
<br />
她指出，上海生物制品研究所生产的VIII因子，因为没有灭活处理，所以带有艾滋病和C型肝炎病毒，尽管当时已规定停产，但仍然继续销售，她儿子用了VIII因子才感染艾滋病的。<br />
<br />
<strong>为此，她和同样不幸遭遇的其它家属，上访、申冤，要求上海生物制品研究所及其上级单位中国卫生部还他们一个公道，照顾他们，可是却常遭到殴打，被关进黑监狱。前两天到国资委递送数据，也是被同样对待。</strong><br />
<br />
<strong>她表示，「我的儿子到现在都不知道他得了爱滋，他说：『妈，世界上最厉害的病就是爱滋和癌症了，可我得的是血液病，怎么也这么难受？妈，下辈子我要做只鸟，能自由自在的飞翔，好不好？』」</strong><br />
<br />
她说，「我们没卖血，没嫖，没娼，我们什么坏事儿也没做，为什么就遭了罪？国家为什么连我们交个材料都不让？我们还能工作，还能为国家出力，为什么就这样对待我们？」<br />
<br />
长期关注这个问题的北京非政府组织「守望家园」负责人苏雨桐表示，<strong>中国大陆血友病患者因为使用上海生物制品研究所生产的VIII因子而感染艾滋病的，现在已知有两百多例，但本案从未得到全面调查，受害者肯定不只此数。</strong><br />
<br />
<strong>今天来到北京南站的受害者家属有20多人，她们不是患者的妻子就是父母、兄弟、姐妹，每个人都戴着口罩，神情凄然，一讲起亲人的遭遇，就忍不住的抽泣、哽噎，与现场官方活动形成强烈对比。</strong><br />
<br />
他们指出，上海生物制品研究所的VIII因子把他们送上人生绝路，这是客观事实，血友病人感染艾滋病是重大血液污染事件，不是天灾，而是人祸，上海生物制品研究所应承担法律责任和民事赔偿责任，同时也应每月给予受害患者生活保障金营养补助费人民币3000元，以解燃眉之急。<br />
