																											<div id="postmessage_176441" class="t_msgfont">拒付中介费？ 易中天：买别墅是自己努力的结果<br />
<br />
2008年07月21日13:16 <br />
 <br />
<br />
　　靠《品三国》攒了点钱的易中天去年在厦门用500万买了一套别墅，却给他带来了大麻烦。几天前，厦门一家房产中介公司声称，易中天买别墅，拒付中介费。昨天，易中天接受了本报记者的书面采访，他称，中介商的指责“太有才了，可以写小说”。<br />
<br />
　　事件<br />
<br />
　　易中天拒付中介费？<br />
<br />
　　整件事情看上去似乎并不复杂。这家厦门房产中介商前天接受当地一家媒体采访时表示：去年10月，他们带着易中天和易夫人去看房，结果房主一眼就认出了易中天，于是偷偷给易中天留了电话号码，没想到，易中天就直接和房主联系起来，最后撇开了中介商，买下了那栋别墅。眼看着13.8万元的中介费没了，中介商当然不愿意了，找到了易中天理论后，易教授只肯拿出5000元了事，这当然不能让中介商满意。<br />
<br />
　　此事被捅出来之后，一向名声不错的易中天顿时陷入了“不义”的境地，有一些人想不明白，一个身价几百万的大学名教授，竟然会做出这种事情。而另一些人则认为中介商显然在借此进行炒作。<br />
<br />
　　易中天<br />
<br />
　　5000元是“信息费”<br />
<br />
　　本报记者昨天联系到了易中天，并在中午得到了他的一个书面答复。成名后，易中天决不轻易接受采访，对一些争论，也总是一笑了之，不过这回，易中天有点气愤了。<br />
<br />
　　他昨天告诉记者，在厦门买别墅确实有这么回事，但最后能买下来，全部是他自己努力的结果，和中介商无关。据易中天介绍，他和夫人都看中了这套别墅，可房主一开始根本就不想卖，“后来，我和北京一位朋友说起此事，这位朋友碰巧与房主同一行业，有许多共同的朋友，便自告奋勇帮我们斡旋，并促成了我们和房主的首次见面。在这位朋友的帮助下，我们才购得此房。”当然，易中天也承认，他当时能够知道有这套别墅，还是中介商的功劳，他同意支付的5000元就算给他们的“信息费”。“这超标准支付的5000元，难道还算少吗？”<br />
<br />
　　不过令易中天百思不得其解的是，当初提供信息的中介，和现在打横幅讨债的中介，竟然不是一家公司，“眼睛一眨，老母鸡变鸭，有这么变戏法的吗？”而对于中介所说易夫人签过一份有法律效力的《房产求购委托书》，易中天也矢口否认，“那只是一份空白的《看房书》。”<br />
<br />
　　中介<br />
<br />
　　不排除上法庭<br />
<br />
　　易中天明确表示不怕打官司，这让那家中介商有点不可思议，“拒付难道还有理由吗？”该中介公司的一位工作人员告诉记者，他们带易中天夫妇去看了房，并在他们的介绍下，易中天和房主家人见了面，最终交易成功，说到底这就是中介行为，易中天理应支付中介费，而不是区区5000元“信息费”。至于易中天所说的“老母鸡变鸭”，该中介表示，两家中介商是合作的关系，根本不影响索取费用。该中介商还表示，不排除将易中天告上法庭。<br />
<br />
　　一位合同法律师昨天告诉记者，从中介商的表述，可以认为中介行为成立，但是由于中介商既没有原房主的《房产卖房委托书》，也没有易中天的《房产买房委托书》，所以不能证明中介的行为，打赢官司的可能性不大。记者 郦亮 青年报</div>

							
							
							
							
