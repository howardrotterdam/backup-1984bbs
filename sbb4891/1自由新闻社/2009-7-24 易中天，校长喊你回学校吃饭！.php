 <br />
或许易中天忘了：其自己就是一个“出来混的”……<br />
 <br />
<br />
 先向易中天教授提一个“愚蠢”的问题：就是一年前您买别墅的时候欠中介公司的13万中介费，后来“还”给人家没有？就算您出示证明您曾为四川地震灾区捐款8万，那总的核算下来您还“欠”着5万呢！<br />
 <br />
再向易中天教授通报一个真实的数据：就是您做客的那期北京科教频道《非常网络》节目，北京地区收视率只有区区1.54，在当天北京地区所能接收到的节目中仅排名第54位，几乎是这档节目开播半年多以来的“历史新低”。<br />
 <br />
“1.54”和“54”（这两个数字读起来真够丧的！），说明什么呢？说明易中天您老在大部分观众眼中已经不再那么“如日中天”了，访问您的节目已经没有什么“必看性”了，这不，即便您在录制现场频频发飙的镜头后期多半保留播出，都没让收视率飙起来，我想事后编导一定会觉得自己很“彪”――早知如此当初随便找位营养专家，随便讲讲诸如夏季饮食的话题，收视率随随便便也能到2.0啊！<br />
 <br />
据相关媒体报道：“砸场”事件的导火索是主持人问易中天有没有想到《品三国》图书火爆等话题，易中天以“不要以为什么事儿都是策划的，媒体很弱智，总喜欢问动机”作答；此外，在被问到“与余秋雨的网络战是不是为了保持名人热度”等问题时，易中天干脆说，“我拒绝回答愚蠢的问题。<br />
 <br />
易中天还多次以讥讽的口吻问主持人还要继续愚蠢下去吗？嘲讽应该开个主持人培训班；质问主持人怎么就是不明白自己的意思等等。两位主持人几度被噎到无语，特写镜头中甚至能看到他们面露难色、眼泛泪光……<br />
 <br />
虽然易中天教授是靠上电视节目一夜成名，但其显然不是电视节目的制作者，最多是个“活道具”，既然是道具不论死活都不足以充分了解电视节目的制作规律，比如类似名人访谈这样的节目，访谈内容自然要围绕着所请嘉宾且大体分成两部分――名人成名的历史和名人成名后的现实。<br />
 <br />
我不认为主持人问的让易中天现场突然暴怒的两个问题有多么“愚蠢”：“有没有想到《品三国》图书火爆”――《品三国》的火爆难道不是易中天成名的“历史”？“与余秋雨的网络战是不是为了保持名人热度”――与余秋雨的骂战难道不是易中天成名后的“现实”？<br />
 <br />
观众中对易中天不感兴趣的人，对有关其的一切问题都不感兴趣；而对易中天感兴趣的人，难道对上述两个问题不感兴趣？如果主持人问出的是收看节目对易中天感兴趣的那部分观众所感兴趣的问题，何况既不低俗也不八卦，怎么就“愚蠢”了呢？前些日子您易中天不也蓦地站出来向余秋雨提了三个有关“捐款门”的问题吗？那些问题不也是关注此事件的受众想了解的吗？您当时怎么就不觉得自己问的那几个问题很“愚蠢”呢？<br />
 <br />
“不问动机，不问资格，不问对象”，这是日前易中天借攻击余秋雨之机祭出的“批评自由三不问”（果真吟得一首好打油诗！），好一个“三不政策”！问题是您批评他人的时候提倡不问动机，不问资格，不问对象，可您自己又是怎么对待向您发问（远远还没到批评、指责、攻击）的两位主持人的呢？<br />
 <br />
节目录制现场――<br />
 <br />
您难道没臆断主持人提问的动机？如果您不认为他们“动机不纯”，您至于突然发飙吗？<br />
 <br />
您难道没在意主持人提问的资格？如果您不认为他们“才疏学浅”，您至于恶语相向吗？<br />
 <br />
您难道没在乎主持人提问的对象？如果你不认为他们“胆大犯上”，您至于恼羞成怒吗？<br />
 <br />
“己所不欲，勿施于人”，没想到您一靠讲国学起家的名教授，讲台上话音刚落，这么快就把中国国学史上最核心的人文思想都给淡忘了！<br />
 <br />
 <br />
