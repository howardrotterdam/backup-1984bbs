													<h2>解放军罗少将：美已对我朝形成“满月形”包围</h2>
						
						
																											<div id="postmessage_746802" class="t_msgfont">解放军少将称美已对中国形成“满月形”包围<br />
<br />
2010年07月18日08:04<br />
<br />
<img src="http://img.hexun.com/2010-07-18/124282627.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　罗援,军事科学院世界军事研究部副部长,少将,研究员,博士生导师,中国军事科学学会常务理事兼国际军事分会会长。<br />
　　近日,美国海军三艘核动力攻击潜艇同时在亚太露面炫耀武力:“佛罗里达号”停靠印度洋前哨迪戈加西亚港,“密歇根”号停靠韩国釜山港,“俄亥俄”号停泊在菲律宾苏比克湾。<br />
<br />
　　罗援少将:美韩军演给周边地区带来的影响不可低估<br />
<br />
　　日前,中国军事科学学会副秘书长罗援少将在与人民网网友在线交流时指出,美韩军演给周边地区带来的负面影响不可低估,中方应该高度警惕的是,美国从没放弃针对中国的围堵,目前已形成“满月形”包围、三道封锁线。<br />
<br />
　　1 美韩军演制造了新的危机和障碍:“天安”舰事件引起的危机没有解决,美国又在朝鲜半岛制造了新的危机;中美军事交流的三大障碍没有排除,美国又制造了新的障碍。<br />
<br />
　　2 对敌意行动要有针锋相对的反制措施:中国现在的周边环境并非太平无事。我们要有忧患意识。对一些针对中国的含有敌意的行动,要给予针锋相对的反制措施。<br />
<br />
　　3 在南海宣示主权要体现军事存在:有些国家屡屡违反南海行为准则,他们对我们提出的“搁置争议、共同开发”有一个片面的理解,认为我们搁置的不是争议,搁置的是主权。“共同开发”变成了“排华”的单向开发。<br />
<br />
　　美国在中国周边地区抢占战略支撑点 航母战斗群将不来黄海<br />
<br />
　　美韩军演牵动人心,新华社7月16日报道称,军演将于本月下旬在日本海率先展开;美国媒体报道称“乔治・华盛顿号”航母战斗群将不会出现在黄海,而是在日本海参加演习。不过,此次军演给中国周边地区带来的负面影响仍然不可低估。<br />
<br />
　　中国军事科学学会副秘书长罗援少将在接受人民网采访时指出,美国的“密歇根”号核动力攻击潜艇到了釜山,“俄亥俄”号到了菲律宾的苏比克基地, 还有“佛罗里达”号到了印度洋的迪戈加西亚岛。这些美国核动力攻击潜艇的调动,要把它放在整个美国战略布局上来看。以前美国采取“双头鹰政策”――既看住欧洲,又看住亚洲,现在它把整个军事部署朝亚太地区倾斜了。<br />
<br />
　　美国对华形成“满月形”包围<br />
<br />
　　通过一系列部署,美国在第一岛链对中国进行围堵,加上在关岛地区、夏威夷地区,形成对华三道封锁链。有人说美国对华形成了C形包围,但现在美国和一些西方国家,对华已不是C形包围了,而是 “满月形”包围,(在中国周边)东西南北都摆点布势,像下围棋一样,在这些地区抢占战略支撑点。美军将60%的攻击潜艇都计划部署到亚太地区,另外,还计划把战略轰炸机部署在关岛。对此,中方应该高度警惕。<br />
<br />
　　近期,以美国为首的14国进行了“环太平洋(601099,股吧)2010” 军演,里面没有中国。美国在中国周边搞了很多联合军演,据一些媒体披露,在联合军演中都把中国作为假想敌。美国海军潜艇司令杰伊在澳大利亚曾说过,中国海军是一个不断增长的威胁,美国海军将调集60%的核潜艇进驻太平洋地区,它的针对性非常明确。这次14国联合军演,除了一些联合搜救之外,一个重要的科目就是演练反潜。<br />
<br />
　　中国对敌意行动要有反制措施<br />
<br />
　　罗援指出,中国现在的周边环境并非太平无事,我们要有忧患意识、国防意识。面对“包围”,中国在应对上要体现出“大的格局、大的思考、大的战略”。一方面要加强自己海空力量的建设,另一方面要在周边建立一些战略支撑点,经营好周边安全环境。另外,对一些针对中国的含有敌意的行动,要给予针锋相对的反制措施。 (人民)<br />
<br />
　　罗援:要在南海体现我们的行政、法律、军事和舆论存在<br />
<br />
　　“搁置争议”不是“搁置主权”<br />
<br />
　　到底什么是中国的核心利益？7月13日,中国外交部发言人秦刚明确做出界定,“国家的主权、安全、领土完整和发展利益都属于中国的核心利益”。中国军事科学学会副秘书长罗援少将在与人民网网友在线交流时表示,要确保我国在南海的核心利益问题,我们一定要宣示我们的主权。<br />
<br />
　　罗援表示,在南海问题上,我们对外宣示主要有两句话,“搁置争议、共同开发”。但是现在的问题是,我们积极主张用和平方式、用外交手段解决南海的一些争议问题,和相关国家签署了一个南海共同行为准则,就是《南海共同宣言》,我们认真地执行着这个行为准则,但是有些国家屡屡违反南海行为准则,派他们的一些国家领导人到一些有争议的地区进行主权的宣示。<br />
<br />
　　罗援说,他们对我们提出的“搁置争议、共同开发”有一个片面的理解,认为现在搁置的不是争议,搁置的是主权,就是把我们的主权搁置下来。另外, 现在我们讲的是共同开发,而现在变成“排华”的单向开发。这就带来了问题,所以在这个地方,我们一定要宣示我们的主权。<br />
<br />
　　在南海宣示主权,罗援提出了四个方面的建议:<br />
<br />
　　第一,体现我们的行政存在,就是要在有争议地区设市、设县、设镇,明确这是在我的行政区划之内的,另外要任命一些行政领导人,有些岛礁我们是去不了的,要规定谁管辖它,要有行政管辖权,要定期地进行张榜公示。<br />
<br />
　　第二,法律存在。必须明确这九条断曲线,到底是什么样的性质。它是我们的领海线,还是我们的行政管辖线,要有法律上的界定,这个问题不能拖得太久。有些人说现在我们的九条断曲线离其他国家可能太近了,是不是做一些修改,我认为这里面有一个很关键的问题就是曾母暗沙,它现在已经不是一个自然的符号,已经变成一个政治符号了。<br />
<br />
　　第三,体现军事存在。要在能派兵的地方派兵驻守,不能派兵的设一些军事设施,比如观通站、气象站、灯塔,如果这些不能设,至少要设立主权碑,能悬挂中华人民共和国国旗的地方要悬挂国旗,另外还可以设立一些主权象征的人文符号。我们的军舰要定期不定期地在这个地区进行巡航,也是显示我们的主权存在。<br />
<br />
　　第四,舆论存在。我们的公民包括在座的网友,大家在不同的场合,利用不同的媒体,要宣示我们的主权,我们的主权不允许任何人染指。<br />
