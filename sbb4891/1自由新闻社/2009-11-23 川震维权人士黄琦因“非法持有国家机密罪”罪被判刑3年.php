																											<div id="postmessage_341539" class="t_msgfont">黄琦涉嫌非法持有国家机密文件案一审辩护词<br />
黄 琦 涉 嫌 非 法 持 有 国 家 机 密 文 件 一 案<br />
<br />
*一 审 辩 护 词*<br />
<br />
*（黄琦辩护律师莫少平、丁锡奎提交）*<br />
<br />
*成都市武侯区**人民法院刑事审判庭*<br />
<br />
*尊敬的税长冰审判长，黄奕、朱军良审判员：*<br />
<br />
我们受本案被告人 *黄琦 *的委托和 *北京莫少平律师事务所 *的指派，在黄琦涉嫌非法持有国家机密文件罪一案中担任其辩护人。我们将忠实履行*<br />
《中华人民共和国刑事诉讼法》*（以下简称《刑诉法》）<br />
第35条规定的辩护人的职责，依法维护黄琦的合法权益。接受指派后，我们多次会见黄琦，查阅了检察机关移送法庭的证据材料，并进行了必要的调查取证工作，刚刚又参与了法庭调查和庭审质证，使我们进一步了解了本案的事实情况。在尊重事实和证据的基础上，现依据相关的法律规定提出如下辩护意见，供法庭评议时参考：<br />
<br />
*辩护人认为*：成都市武侯区人民检察院（以下简称“公诉机关”）成武检刑诉字[2009]第10号《起诉书》（以下简称《起诉书》），指控黄琦犯有*<br />
非法持有国家机密文件罪*，*事实不清，证据不足，程序存在诸多瑕疵*；该指控不能成立。具体理由分述如下：<br />
<br />
*一、现有的控方证据不能证明是黄琦持有国家机密文件*<br />
<br />
1、通过庭审调查可以得出结论，黄琦至多只是涉案的三星移动硬盘（以下简称“涉案硬盘”）的*所有权人*（即涉案硬盘是由其出钱让蒲飞、徐淑蓉购买的）<br />
，但现有的*控方证据不能*证明黄琦是涉案硬盘的实际*占有、使用*和*控制人*。根据《中华人民共和国刑法》（以下简称《刑法》<br />
）规定构成非法持有国家机密文件罪的*行为人*，必须是国家机密文件载体的*实际占有、使用和控制人*。<br />
<br />
在此需要强调的是，公诉人在庭审发言时，混淆了“所有权人”与“使用人”、“占有人”、“控制人”之间的界限，将“所有权人”等同于“使用人”、<br />
“占有人”或“控制人”，这是有违基本法理和生活常识的，比如，在房屋出租时，房屋的所有权人并不是房屋的实际占有、使用和控制人。<br />
<br />
2、有证据证明成都市武侯区沙堰西一街23号康华医院宿舍楼3楼1号（以下简称康华医院宿舍楼），既是黄琦的*住所*，又是“六四天网”义工的*工作场所<br />
*，平常来往的人很多，不能排除有其他人的物品也放在该处（在公诉机关移送法庭的案卷材料中辩护人没有看到侦查机关将涉案硬盘交由黄琦*辨认*<br />
的记录，遗憾的是，庭审时合议庭也没有将涉案硬盘实物交由黄琦辨认；还需要强调的是，侦查机关并未对到过康华医院宿舍楼的全部“六四天网”义工进行调查）。<br />
<br />
*3、根据辩护人向法庭提交的*证据可以证明黄琦不是涉案硬盘的唯一占有、使用和控制人（据黄琦讲，他本人从不使用移动硬盘），<br />
而且黄琦当庭讲，他曾经多次要求侦查机关对涉案硬盘上是否留有他的指纹进行鉴定，但侦查机关未予理睬；辩护人认为，法庭应依法满足黄琦提出的指纹鉴定要求，以期查清案件事实真相。<br />
<br />
4、据黄琦讲，购买涉案硬盘的时间是在2008年1月前后，而《电子证物检查工作记录》记载：三份涉密文件的存储时间是在*2007年10月以前*<br />
，涉案硬盘未买，就已经存入了涉密文件，这显然是违背常理的<br />
（辩护人庭审前曾请求合议庭传包括蒲飞、徐淑蓉二人在内的多名证人出庭作证，目的之一也是为了核实该情节；令人费解的是，合议庭拒绝了辩护人这一请求；还需要指出的是，庭审时公诉人对辩护人提交的证人证言的真实性提出了质疑。这就导致了，一方面公诉人质疑“证人证言”的真实性，另一方面合议庭又不许证人出庭作证，这种做法如何能公正客观查明事实真相？）<br />
。<br />
<br />
*二．现有的控方证据不能证明黄琦具有非法持有国家机密文件的主观故意*<br />
<br />
根据《刑法》规定：非法持有国家机密文件罪中，行为人*主观方面必须是直接故意*。*主观方面主要包括认识因素及意志因素，*从*认识因素*<br />
来看，行为人必须认识到自己持有的文件属于国家秘密；从*意志因素*来看，行为人希望该国家机密文件处于自己的控制之下。具体到本案，<br />
<br />
1、现有的控方证据不能证明黄琦案发前看过涉案硬盘里所谓的机密文件，所以更无从谈起黄琦*明知*涉案硬盘里的三份文件属于国家机密文件。<br />
<br />
2、有证据证明，涉案硬盘是属于公用的，“六四天网”义工都可以使用；因此，使用过涉案硬盘的“六四天网”义工都可以用其*下载文件*<br />
或者对其中的文件进行复制、增加或删减，黄琦无法将硬盘处于自己控制之下（见辩护人提交的郑大靖、田永德的证言）。<br />
<br />
3、黄琦已向公安机关说明（见黄琦2008年6月26日的《讯问笔录》），“如果硬盘里有涉密文件，就是‘sg’发给他的”，由此看出，黄琦并不具有“*<br />
拒不说明机密来源*”的情节。<br />
<br />
另外，庭审时公诉人一再强调黄琦是“六四天网”的负责人，辩护人认为，非法持有国家机密文件罪不涉及*单位犯罪*<br />
，黄琦所担任的职务与本案无关，何况也没有充分的证据证明黄琦就是“六四天网”的负责人。<br />
<br />
*三、涉案硬盘里的三份文件不属于国家机密*<br />
<br />
（一）*涉案硬盘的三份文件属于应当或已经**公开的信息*<br />
<br />
根据相关法律，*“国家秘密”是指关系国家的安全和利益，依照法定程序确定，在一定时间内只限一定范围的人员知悉的事项”*<br />
（《中华人民共和国保守国家秘密法》第二条）。<br />
<br />
由此可知，国家秘密的本质属性就是*“秘密性”*即*“不公开性”*，*在特定的时间，只限特定的人知悉*<br />
。那么如果一个信息是应当或者已经公开的，就不可能是秘密。<br />
<br />
1、《党的十七大期间信访稳定工作方案》（以下简称《稳定工作方案》）、《关于举办“三个讲清楚”学习班的方案》（以下简称《学习班方案》）<br />
是应当公开的信息<br />
<br />
信访稳定工作，是政府的日常工作之一，其是否依法行政，应当接受人民群众的监督。《稳定工作方案》、《学习班方案》涉及公民切身利益，反映行政机关职能、办事程序等情况，根据<br />
*《中华人民共和国政府信息公开条例》*第一条、第九条规定，是应当公开的信息。<br />
<br />
辩护人提交法庭的《补充证据》中《关于认真实施十七大期间安镇镇信访稳定工作方案的通知》（江苏省无锡市锡山区安镇镇信息公开网公开发布，<br />
<a href="http://zwgk.jsxishan.gov.cn/bd03/b/07/7062.shtml" target="_blank">http://zwgk.jsxishan.gov.cn/bd03/b/07/7062.shtml</a>）<br />
与《稳定工作方案》相比较，除发布主体，受文单位不一致外，其主要内容雷同。故，《稳定工作方案》不仅是应当公开的信息，而且已经被间接公开，毫无秘密可言。<br />
<br />
2、《*中共中央政法委员会*关于印发〈涉法涉诉信访责任追究规定〉的通知 》（以下简称《责任追究规定》）是应当并且已公开的信息<br />
<br />
《责任追究规定》在网络上能检索得到，该*文件发布日期为2006年3月9日，下发之日起执行，*<br />
属于党内法规性质的规范性文件，只有公开，才能执行；而且该文件所依据《公务员法》《信访条例》《纪检条例》均是公开的，针对的知悉对象是不特定的，与国家秘密知悉对象是特定的相矛盾；特别是公开该文件，有利于监督各级相关领导更好的履行职责，对国家利益是有利的；故，《责任追究规定》是应当并且已公开的信息。<br />
<br />
综上所述，涉案硬盘的三份文件属于应当或已经公开的信息，应当知悉此信息的人是不特定的、众多的，其不符合国家秘密的特定属性，因此不可能是国家秘密。<br />
<br />
*（二）从实体上及宪政的角度看，上述三个文件的内容不应属于国家秘密而应属于公民知情权所应知悉的内容。*<br />
<br />
公民对国家机关掌握的信息（包括关系到公民基本权利、政府日常工作、公务人员责任追究等方面）享有了解和知悉的权利，即公民的*知情权。*<br />
知情权是公民参与管理国家事务及参与管理经济文化、社会事务的一个前提，是公民享有权利和履行义务的必要条件，因而，知情权是公民的基本权利。*这是《*<br />
中华人民共和国宪法*》及我们国已经签署加入的许多国际公约*确认的一项公民基本权利。<br />
<br />
保障公民的知情权需要信息公开，*没有公开就没有正义，没有公开就没有民主*，*没有公开就没有法治*，与信息公开相对的是保守秘密<br />
（就本案而言主要是指国家秘密），现代法治国家执政理念是：*信息公开是原则，保守秘密是例外；*<br />
<br />
从本案涉及的“机密文件”<br />
来看，其内容只是政府依法行政应当公开的信息，其与国家的安全和利益无关，不属于《中华人民共和国保守国家秘密法》第八条中列举的“关系国家的安全和利益”<br />
的事项，完全是公民应当知情的内容。四川省及江苏省国家保密局将上述三份文件鉴定为“*机密级国家秘密*<br />
”，缺少事实及法律依据。辩护人在此再次郑重提出对涉案硬盘中的三份文件重新进行鉴定的申请，鉴定单位应当是中华人民共和国国家保密局。<br />
<br />
*四、本案的侦查、审查起诉及审判程序均存在诸多瑕疵甚至违法*<br />
<br />
*（一）侦查程序*<br />
<br />
*侦查机关不批准会见黄琦违反《中华人民共和国律师法》**（*以下简称《律师法》*）**的规定*<br />
<br />
2008年6月23日辩护人曾当面向成都市公安局武侯区分局递交了本案会见手续。翌日，该局书面答复，因本案“涉及国家秘密”，决定不批准会见黄琦。后又多次联系会见事宜，均无答复。<br />
<br />
*辩护人认为，*基于“*新法优于旧法”*原则，根据新修订的《律师法》第三十三条的规定，我们会见黄琦是无须经侦查机关批准的。<br />
<br />
*（二）审查起诉阶段*<br />
<br />
*1、公诉机关不允许律师摘抄、复制与案件有关的卷宗材料违反《律师法》的规定*<br />
<br />
审查起诉期间，辩护人去公诉机关阅卷，要求复制有关案卷材料，本案承办人*李艳萍*检察官说:<br />
本案涉及国家秘密，只允许摘抄部分程序性文书，不允许复制卷宗材料；我们认为公诉机关上述做法明显违反《律师法》34条关于<br />
“受委托的律师自案件审查起诉之日起，有权查阅、摘抄和复制与案件有关的诉讼文书及案卷材料”<br />
的规定；公诉机关坚持以案件涉密为理由，不允许律师摘抄、复制与案件有关的卷宗材料，实质上是对律师行使辩护权设置了障碍，变相剥夺了律师的辩护权！<br />
<br />
*2、公诉机关没依法向法庭移送《证据目录》及《证人名单》*<br />
<br />
根据《刑诉法》第一百五十条及《最高人民法院关于执行〈中华人民共和国刑事诉讼法〉若干问题的解释<br />
》第一百一十六条第一款第（四）、（六）项的规定，公诉机关应当向法院移送《证据目录》及《证人名单》，并且法院也有审查义务。公诉人庭审时所称<br />
“全案移送，无需向法院移送《证据目录》及《证人名单》”，没有法律依据！而且公诉机关的这种做法，等于给辩护庭审质证设置了障碍。<br />
<br />
*（三）审判阶段*<br />
<br />
*1、贵院不允许律师摘抄、复制与案件有关的卷宗材料违反*《刑诉法》及*《律师法》*<br />
<br />
案件移送贵院以后，辩护人来阅卷，要求复制有关案卷材料，参与本案法官助理*雷星*说:<br />
本案涉及国家秘密，只许查阅，不允许摘抄、复制卷宗材料，并当场将辩护人的笔没收；<br />
虽然贵院开庭前允许辩护人复制了有关卷宗材料，但仍有部分证据包括程序方面卷宗材料仍被拒绝查阅复制，使得辩护人无法对贵院是否超审限做出判断。辩护人认为贵院上述做法明显违反《刑诉法》第三十六条第二款<br />
“辩护律师自人民法院受理案件之日起，可以查阅、摘抄、复制本案所指控的犯罪事实的材料”及《律师法》34条关于<br />
“受委托的律师自案件被人民法院受理之日起，有权查阅、摘抄和复制与案件有关的所有材料”<br />
的规定；贵院以案件涉密为理由，不允许律师摘抄、复制与案件卷宗材料，没有任何法律依据，实质上是对律师行使辩护权设置了障碍，变相剥夺了律师的辩护权！<br />
<br />
*2、贵院拒绝证人出庭不利于查明案件事实*<br />
<br />
本案控辩双方对事实争议比较大，只有证人出庭作证，才能进一步的查明事实真相，也遵循了刑事诉讼的基本原则――直接言词原则，贵院以<br />
“控方证人已提供书面证言，足以作为定案依据，辩方证人不能确定其与本案是否有关系”为由拒绝证人出庭作证，这既不利于查明事实真相，又无法理依据，更有失公允！<br />
<br />
*3、公诉人侵犯了被告人黄琦的最后陈述权*<br />
<br />
被告人黄琦在做最后陈述时，数次被公诉人以抗议的形式所打断；辩护人认为，《刑诉法》及相关司法解释并没有赋予公诉人抗议被告人最后陈述的权力，只有法官才有权力判断被告人的最后陈述是否不当并决定是否予以制止，公诉人的做法，实际上已经构成了对被告人黄琦最后陈述权的严重侵犯。<br />
<br />
*尊敬的审判长、审判员：*<br />
<br />
我国正处于一个法治不断完善的过程中，在此过程中对于一些问题的看法有分歧是非常正常的，特别是在对待某些*罪与非罪*<br />
的问题上更是如此，本案案情并不复杂，但从宪政的角度看，本案涉及到*公民知情权、国家秘密*相互关系问题（*<br />
包括法院是否有权对“什么是国家秘密”及“国家秘密的密级”做出终局裁决的问题*），<br />
故引起了国际社会及国外众多媒体的极大关注，并作了大量报道，但辩护人始终坚信，*有利于尊重和保障基本人权的判决才是公正的判决，才是经得起历史考验的判决！*<br />
<br />
综上，辩护人认为《起诉书》指控黄琦犯有非法持有国家机密文件罪不能成立，请贵院依法查明事实真相，依法判处黄琦无罪。<br />
<br />
*黄琦一审辩护人：北京莫少平律师事务所***<br />
