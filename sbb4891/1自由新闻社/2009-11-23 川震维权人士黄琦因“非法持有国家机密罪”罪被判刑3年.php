不给邮寄判决书的做法明显违反有关法律规定，现提出如下律师意见：<br />
<br />
1、《关于执行〈中华人民共和国刑事诉讼法〉若干问题的解释》（1998年9月2日最高人民法院公布，以下简称《刑诉法解释》）第一百八十二条规定<br />
“……判决宣告后应当立即将判决书送达当事人、法定代理人、诉讼代理人、提起公诉的人民检察院、辩护人和被告人的近亲属……”。<br />
<br />
可见，将判决书送达辩护人是法院的义务，最高人民法院的《刑诉法解释》都给予了明确规定，贵院不给辩护人送达判决书是违反有关规定的。<br />
<br />
2、《中华人民共和国刑事诉讼法》第八十一条第一款规定<br />
“送达传票、通知书和其他诉讼文件应当交给收件人本人；如果本人不在，可以交给他的成年家属或者所在单位的负责人员代收”；<br />
<br />
可见，法律的立法本意是，法院送达包括判决书在内的诉讼文书给辩护人，是应当派员到辩护人的住所送达，而不是相反，令辩护人去法院领取。<br />
<br />
3、《刑诉法解释》第一百零五条规定“直接送达诉讼文书有困难的，可以委托收件人所在地的人民法院代为送达，或者邮寄送达”。<br />
<br />
可见，邮寄送达是法院送达包括判决书在内的诉讼文书给辩护人的一种方式，辩护人的要求是符合法律以有关司法解释的规定的。<br />
<br />
