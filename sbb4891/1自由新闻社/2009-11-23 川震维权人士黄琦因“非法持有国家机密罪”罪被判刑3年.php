我们是北京莫少平律师事务所的 *莫少平、丁锡奎 *律师，是贵院办理的“*非法持有国家机密文件罪”*一案被告人 *黄琦** *的辩护人。<br />
<br />
2009年11月19日，丁锡奎律师接到贵院刑庭张书记员电话，说黄琦案将在11月23日上午十点宣判，通知我们出庭，并应我们的要求传真来了《出庭通知书》。当时丁律师与张书记员约定：由于路途遥远，为了节省开支以减轻当事人经济负担，辩护人就不出庭了，宣判以后将判决书邮寄送达辩护人。11月23日下午，丁律师与张书记员通电话，询问判决书邮寄情况，张书记员说：<br />
