													<h2>60亿握手费李嘉诚搞掂胡锦涛内幕</h2>
						
						
																											<div id="postmessage_832863" class="t_msgfont">来源：壹周刊20100916<br />
上周一是深圳经济特区成立三十周年，这个官方的庆祝典礼，却出现瞩目的历史一刻。会场内，国家主席胡锦涛，竟亲自步近上市公司长实主席李嘉诚，主动握手会面，双方寒暄长达十分钟。相对一众大I沙只能在台下拍手和应，特首曾荫权亦只能靠边站，诚哥这铺要算又一“代表作”。 <br />
<br />
据知，原来胡李会面，关键在于李嘉诚在此前捐了一笔达六十亿元的巨款给国家，胡锦涛“感激”得亲自表扬。一直以来，在胡锦涛与上任江泽民间的政治角力中，李嘉诚是后者“核心”人马，故这次李嘉诚以巨额握手费将胡锦涛也“搞掂”，标志着政治手腕一向了得的李嘉诚，由中国改革开放舵手的邓小平，到执政强硬的江泽民，以至现时对商界无甚好感的胡锦涛，三朝都沟通得到！政商两瓣皆掂，李嘉诚势在内地路路畅通，然而这却种下了一人独大的遗祸。 <br />
<br />
这个“世纪握手”，出现于上周一的深圳经济特区成立三十周年庆典。早于庆典举行前，李嘉诚已由深圳市委书记王荣亲自邀请入座，及至胡锦涛由国务院副总理王岐山、广东省委书记汪洋等陪同下进场时，即一个箭步找李嘉诚握手，冷不防还热情地说：“我说不管时间长短，我总是要见一见李先生，跟李先生聊一聊。” <br />
<br />
胡锦涛专程来深圳出席特区成立三十周年庆典，发言时笑容满面的李嘉诚身处主席台第三排。能坐上主席台的港人只有他和政协副主席董建华及特首曾荫权。（路透社） <br />
<br />
江泽民与李嘉诚握手，意味李有大靠山直达天庭。 <br />
<br />
李嘉诚曾与邓小平儿子邓质方合作，又捐献予其另一儿子邓朴方的残疾人福利基金，深懂与国家领导人打关系之道。<br />
<br />
献促成胡李会谈 <br />
胡锦涛这时容貌仍显得僵硬，但受宠若惊的李嘉诚已笑得见牙唔见眼。两人特意谈到李的家乡汕头，胡又希望李嘉诚继续发挥影响力，为保持香港繁荣稳定及国家改革开放作出“更多”贡献，凸显李嘉诚责任重大。会面尾声，胡锦涛更祝福李嘉诚“身体健康、阖家幸福”。整个会面长近十分钟，连中央电视台“新闻联播”也罕有地评述内容。 <br />
胡李会面，震惊中外，事关由共产党主政的中国，国家元首竟然高格调地会见一个香港富豪，连香港特区行政长官曾荫权也要靠边站，背后源自一笔来自李嘉诚的六十亿元捐款。 <br />
<br />
而其实早在上月初的长和系业绩会上，李嘉诚已透露了一点玄机。当日有记者问及李嘉诚有关香港社会仇富心态时，他回应指：“君子爱财，取之有道，更要用之有道。”顺势自爆他的李嘉诚慈善基金会，过去三年已捐出逾五十二亿元，但在未来十年，势将加码动用超逾一百亿！多于过去三十年的总和。 <br />
<br />
而据一名知情人士说，李嘉诚捐出的该笔六十亿元巨额，是捐予内地最大的慈善机构“中华慈善总会”，“呢个系一个契机，胡锦涛觉得李嘉诚做了一个贡献祖国的好榜样，所以高调见谝灾感谢，并同时希望其他香港及内地富豪能够效法。”一名政界人士说。根据中华慈善总会网页资料，其隶属于国家民政部，九四年由前统战部长阎明复及前民政部长崔乃夫等创办，会中的领导都是国家前副部长级要员。该会主力救灾、扶贫、助学助医等慈善工作。筹办十多年以来亦只募捐了百亿元，李嘉诚这次一捐便六十亿，自然震动了党政高层。 <br />
<br />
“首长系”高层游走拉线 <br />
<br />
而为这次捐款铺桥搭路的，是加怡集团的董事总经理梁顺生。今年六十七岁的梁顺生，持有纽约州立大学工商管理硕士学位，并拥有三十多年金融、投资及企业管理经验。他并是首长国际、首长四方等“首长系”公司的非执董。“首长系”由长和担大旗，有极浓厚的政治色彩。九十年代初，获国家元老邓小平钦点，由邓的战友周冠五及儿子周北方（有四大太子之称）打骰的北京首都钢铁厂，来港借壳上市，欲与邓及其他太子党打好关系的李嘉诚遂帮忙穿针引线，并主动提出合作。他们借壳联合集团李明治的东荣钢铁，并改名首长。首代表首钢，长代表长实，而首长则有“ leader”之意。<br />
<br />
及后九十年代中江泽民上台，一举将这些北京太子党拉下马，自此首长系变得沉寂。但当时参与收购，在加怡集团打工的梁顺生，则一直与李嘉诚关系密切，后来更取代李氏身边的红人余立发出任加怡董事总经理，近年主力打理李嘉诚的慈善事务。 <br />
<br />
李嘉诚的业务遍布全球，其中在北美洲的赫斯基能源，是和黄旗下的一只金蛋。最近长和则准备以七百亿港元，收购英国电网。 <br />
<br />
位于上海普陀区真如副中心的庞大项目，以几乎二十步一围板的高调姿态，宣传这是属于李嘉诚的“长江实业打造”。（关永浩摄） <br />
<br />
二百亿再注基金 <br />
<br />
梁顺生（左六）极为低调，鲜有公开露面。他养有马匹“再丰裕”，曾拉头马，以前曾养过一匹名为“北大人”的马匹。（《苹果日报》图片） <br />
<br />
除了这次六十亿的握手费捐献，根据民政部发布的《 2009年上半年全国慈善捐赠情况分析报告》显示，○九年上半年海外华人共捐四十亿，单是李嘉诚已占了一半共二十亿元人民币，捐赠额名列第一。据悉，他短期内会再注入二百亿元资金，予他名下的李嘉诚慈善基金会，该会大部分慈善工作俱在内地。李嘉诚慈善基金会在八○年成立，第一个项目便是兴建汕头大学，单是这项目总共捐款二十八亿元，李嘉诚并承诺未来八年再捐二十亿元。基金会现有资产达二百多亿，李嘉诚亦经常注资及四出投资，钱愈滚愈大。李氏亦在早前的业绩会中，透露慈善基金会将于今个月公布新业务。 <br />
<br />
姑勿论如何，这次胡锦涛只与李嘉诚单独会面，让商家如患了红眼症，引来议论纷纷，无限揣测。其中左派元老级人马、前港区人大代表吴康民率先发难，发表文章抨击胡锦涛过度重视香港首富。他更指论对深圳投资有远见的商人，首推合和实业主席胡应湘，因他提出兴建深广高速公路；若论对内地的捐钱，最多的应是邵逸夫，而出力出技术的则有港区人大代表梁秉中和林顺潮。不过，亦有人力撑“胡李会”。<br />
<br />
前政协常委云大棉指，现时能“上得大台”的富豪，舍李嘉诚其谁。“七、八十年代系船王包玉刚，后期系爱国商人霍英东，到霍死后，最掂只系李嘉诚。丈矸荨⒐际视野上早已经抛离其他富豪，晋身为一个国际投资者。”他指○二年李嘉诚曾被推举出任政协副主席，云大棉即教路他拒任，“一个政协副主席职位，只是继总书记、人大委员长、总理等之后的五等芝麻官，要跨级见国家元首，根本无可能。但若然无官职在身，不是任命于中央，则另当别论。”结果李嘉诚接受云大棉建议，翌日宣布推辞出任政协副主席一职，今日的胡李会才有指望。 <br />
<br />
外交强极　买通三朝 <br />
<br />
事实上，李嘉诚政治手腕一流。“诎耸年代已单独会见邓小平，地位超越其他香港富豪如李兆基、郑裕彤等。李兆基纵在股票上大有斩获，但捐钱捐得孤寒，来来去去每次都是三两亿；郑裕彤则私生活较随便，李嘉诚在公在私，层面已高O几班！”一名政协常委说。九十年代邓小平二公子邓质方来港销售“四方西郊花园”住宅，吴光正、徐展堂等富豪纷纷认购“释出善意”，但李嘉诚却是与他成立公司合作，层次更高。九三年，邓质方的上海四方公司，就与和黄合资在上海徐汇区建住宅，并由和黄占六成，上海四方占四成。 <br />
<br />
到九三年李嘉诚一手扶持周北方在香港成立首钢为首的上市公司，包括首长国际、首长四方、首长科技及首长宝佳；李嘉诚的长实及邓质方的四方公司皆成为首长系的股东，同一时间，李嘉诚投资的北京东方广场动工，如今成为北京王府井的地标。 <br />
<br />
到江泽民上场，李嘉诚与属上海帮的江派关系更熟络，江泽民到港主持九七回归庆典时，就下榻李嘉诚旗下的红|海逸酒店，此后成为江泽民到港御用酒店，李嘉诚与儿子李泽钜、李泽楷亲自到酒店与老江吃早餐，俨如一家人。但这关系同时亦为人诟病。 <br />
<br />
○○年，当时特首董建华在未经公开招标下，把现时数码港临海优质地皮免地价批予李泽楷的盈科拓展，令盈科获得兴建四百三十万尺的住宅发展权（即后来的贝沙湾）。这项借科技为名、地产为实的批地行动，明益李家，令八大发展商新地、恒基、新世界等震怒，想过前往北京告“御状”。但李快人一步，抢先到北京与江泽民握手。相片一出，众地产商亦无话可说。九六年李泽钜被绑架，诚哥藉着与江泽民的私交，将儿子李泽钜被张子强绑架的事告诉他，九八年老江一声令下，派出国安部人马出手，在江门将“着草”的张子强生擒，并在广州判处死刑。<br />
<br />
中央大力压抑炒风，李泽楷位于北京盈科中心旁边的土地，闲置三年后转手瑞安建业获利两亿元，今年即被追交罚款，并禁止在北京买地，以儆效尤。 <br />
<br />
李泽楷○六年欲把电讯盈科卖予外资，曾激怒中央，并由老父出手收拾残局。 <br />
<br />
位于北京王府井的东方广场，是邓小平掌政期间，李嘉诚拿下的项目，曾因为兴建高度超越故宫而闹起风波。 <br />
<br />
龟速工程升值三十亿 <br />
<br />
而到胡锦涛○二年当选中共中央总书记以来，却未有如他前任般重视李嘉诚。胡锦涛近年来港都是下榻湾仔君悦酒店，亦未见与李嘉诚单独会面，双方八年关系成僵局。于今年上半年，李家与国家的关系更开始紧张。先是上海、北京，被点名批评有囤地情况，继而今年六月，其红颜知己周凯旋，被内地《财经》杂志报导她卷入内地贪污案，又指她曾付五十万美元“顾问费”予内地商务部高官。就此案，上月周凯旋已入禀高等法院控告诽谤。而有关囤地，表面上被严厉抨击，但本刊到上海、北京两地察看，发现实则未被收回，国家仍对李留有余地。 <br />
<br />
记者到过上海及北京的有关地皮视察，发现购入多年的地皮仍在“养草”，即使赶及符合囤地定义的三年界线前动工，亦以“龟速”进行，其中上海普陀区的项目单单地基打了一整年，其间地价已翻超过一番。 <br />
<br />
该地皮位处的普陀区，集甲级写字楼、星级酒店及高级公寓于一身，是上海四个重点发展的市中心区之一，李嘉诚在北京拥有亚洲最大建筑群东方广场，这正正是东方广场的“上海版”。李嘉诚○六年以二十二亿元人民币买入后一直闲置，至去年四月才宣布动工，目前仍在打地基，预计二○一八年落成，工程期长达九年。根据去年底邻近商业用地的推售价格估算，该地皮约值五十五亿元，换言之，单地价目前已劲赚逾三十亿元。 <br />
<br />
九七年江泽民来港出席回归庆典，下榻刚开幕、分属最顶尖的红|海逸酒店，甚至出席该酒店开幕礼，正突显他与李嘉诚的关系。 <br />
<br />
一九八○年众香港富豪帮忙筹建中国大酒店，当时郑裕彤（左一）、李嘉诚（右三）及胡应湘（右一）亦有出席，至今李嘉诚在国际视野及受中央重视的程度上远远抛离。<br />
<br />
楼盘旁建地铁站 <br />
<br />
另一个李嘉诚旗下位于上海嘉定区的住宅地盘，属全国百大住宅小区之一，地皮○八年购入，由于尚未到三年期限，目前继续“绿草如茵”。该地原本地理位置一般，但已经与当地市政府达成协议，在两个地铁站中间预留地方“加站”，成功为楼盘“创造地理优势”。而在北京，刚于上月被国土资源部点名批评闲置土地的逸翠园地盘，目前仍有一大片地在兴建中。该地○四年购入时，曾签订合同定于○五年初动工，○六年十月三十一日落成，结果项目在年报中刊登的竣工时间一直推后多次，连逸翠园内部销售部门亦不清楚竣工日期。 <br />
<br />
至于其次子李泽楷旗下的盈大，今年三月被指将北京东三环盈科中心大楼旁边的地皮，闲置近三年后转手予“上海姑爷”罗康瑞的瑞安建业获利两亿多元，被列入囤地黑名单，罚款兼禁止在京城买地。该幅地由瑞安接手后，去年八月已即时动工兴建高级住宅，工程现进行得如火如荼，预计明年落成。 <br />
<br />
李嘉诚被“放生”，全因江泽民的影响力仍在。胡锦涛早年上台后，背后仍处处受制于上任江泽民。江泽民○二年从国家主席退下后，仍留任中央军事委员会主席，迟迟未肯把军权交出，时间长达两年，“谙朐傩任军委会主席的，之前更连升二十八个中将希望获得支持，但党内大部分老军委反对，他唯有离职。”一名知情人士说。九九年江泽民建国五十周年大庆，下令不许退休老军头穿军装，在天安门城楼会见党政军要员时，见前中央军委副主席刘华清上将仍穿上军服就上前问到：“不是说不准穿军装吗，你怎么搞的？”属老军头的刘华清回敬他一句：“你一天仗没打过都可以穿军装，我怎么就不能穿！”邓小平临终时把胡锦涛“托孤”予刘华清，希望军方支持胡锦涛，后来江泽民藉着远华走私案，来“整”刘华清的女儿及媳妇来报复；因刘家力撑胡锦涛，双方关系亲密，今年春节，胡锦涛仍亲自到刘华清家中探望老人家。 <br />
<br />
胡应湘投资广东三十年 <br />
<br />
胡应湘在广东省大力扩建公路，建树甚多，但在这次深圳成立三十周年庆典中，踪影全无。<br />
<br />
前港区人大代表吴康民在报章撰文，指对深圳投资有远见的商人，首推胡应湘，早在八○年胡应湘曾联同多位富豪，包括李嘉诚、郭得胜、郑裕彤、冯景禧、李兆基等在中国兴建中国大酒店；深圳联检大楼也是胡应湘倡议发展的。 <br />
九十年代初期胡应湘的合和实业大力发展广东省的基建事业，包括广深珠高速公路、环城公路、虎门大桥及沙角火力发电厂之后，才陆续获香港富豪垂青，争相加入一齐玩，李嘉诚的长实曾入股合和一成股份，李泽钜也加入合和董事局。只是○二年胡应湘倡议兴建港深澳大桥，并且获城中富豪支持，此举威胁到李嘉诚的和黄港口业务，马仔霍建宁狂轰胡应湘，李嘉诚与胡应湘不和关系表露无遗。 <br />
<br />
胡江背后发功 <br />
<br />
在去年十月一日的建国六十周年庆典中，江泽民（右九）站在天安门城楼最中央，与民众挥手，穿军服的元老刘华清（左三）亦有份站在城楼。（路透社图片） <br />
<br />
但江泽民影响力犹在，近年在重大集会场合，如汶川地震追悼、北京奥运、及建国六十周年纪念等，都有胡锦涛与江泽民同时出现的场面，且江泽民是站在胡锦涛与总理温家宝之间，显示“实力”。在○七年十七大会议，江泽民及刘华清一众元老也有份同场出席，此举乃是希望“以和为贵，不要再斗”，可见胡锦涛对江泽民也有所忍让。 <br />
<br />
还有一年半胡锦涛就要退任，现时正是胡江之间挑选下届领导班子布局的关键时刻，尤其今年十月便是五中全会，若江泽民挑选的国家副主席习近平能当军委副主席，局面便明朗化，而胡则想国务院副总理李克强出任总理，但亲江系主管金融的国务院副总理王岐山，现时呼声力压未有重大政绩的李克强，如胡锦涛未能安插到他在未来新班子位置，胡退位后的利益难以保得住。现时江、胡两派正在角力及讨价还价。“胡锦涛并要求与江泽民一样，退任国家主席后留任军委主席两年，当大家扯平才体面下台。”一名知情人士说。所以今次胡锦涛肯与李嘉诚“握手”，外间推断也是江泽民背后发功。 <br />
争相入政界 <br />
<br />
由恒基第二代李家杰牵头的百仁基金会，的确有不少富豪第二、三代参与，但实质影响力不大。（严宝权摄） <br />
<br />
要在中国大陆做生意路路畅通，非捞到政治本钱不可；不少富豪第二代积极为将来铺路，○八年本来坐定粒六，做新一届政协常委的恒地副主席李家杰，名落孙山，虽然恒基捐过数以亿计款项返大陆，但有政协常委透露，并非捐钱就得，必须身体力行做出对国家有利的事。李家杰于是积极搞“百仁基金会”，图统领本港富豪第二代，但成员中未见有级数的第二代，如李嘉诚的两名儿子。<br />
<br />
