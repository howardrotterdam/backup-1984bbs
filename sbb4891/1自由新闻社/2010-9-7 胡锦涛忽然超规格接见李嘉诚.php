																											<div id="postmessage_818379" class="t_msgfont"><a href="http://lawyerpuzhiqiang.blog.sohu.com/159313312.html" target="_blank">http://lawyerpuzhiqiang.blog.sohu.com/159313312.html</a>（图片在博客里）<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;9月6日上午，应召赶往石家庄中级法院被开庭。<br />
<br />
&nbsp; &nbsp; 法院朝南的门是衙门正门，平常不怎么让人进出，公事一律走西侧“旁门”。可这个早上，数十位男女怒放 “诬蔑不实之词”。红色横幅挂在大门左侧树丛，说个别法官贪赃枉法为虎作伥。客户边开车边说法院门口常有人折腾，个把月前来领传票，就见急救车从院里绝尘而出。听说病人是一老头儿，好像刚吞了把剪子下肚。感觉自己心肝，瞬间也揪成一团。<br />
<br />
&nbsp; &nbsp; 转弯开进西侧胡同，不远处是法院旁门，递传票办手续就走这个门。湖南法院饱尝了冲锋枪，让这儿的警戒标准高了，虽说还没增设备安检，但法警慧眼如炬认人不认手续，任你传票身份证律师证，还比不上书记员一嗓子。<br />
<br />
&nbsp; &nbsp; 旁门西北角儿居民楼顶隐约大红字，曰“燕赵之光”，一家律师楼的字号。燕赵自古慷慨悲歌敢叫之光的律师，想必是以推动法制进一小步自命的。我曾经认为挑法院旁门扎营的同行，路数多半靠走左道，打广告塞名片――我在海淀法院门口，就被同行试图指点迷津。但我可能错了，跟两眼一抹黑的百姓，他们其实比我走得近，谁上了轿才扎耳朵眼儿，缓急之间找人代书诉状解答咨询，他们往往能一蹴而就，每回就收百十来块，细水长流集腋成裘。不是“部级文明所”，至少也“青年文明号”。看守所边上摆摊儿的，专陪跑单帮律师“会见”，解的是燃眉之急。<br />
<br />
&nbsp; &nbsp; 靠近路边儿的院子里，墙壁上竖块燕赵之光，路边三个宣传橱窗。一为“荣誉”橱窗，这家所曾被石家庄市司法局和律协“售予”“文明律师事务所”；其二“重要活动”，有人开会有人讲话有人握手；其三“燕赵之光律师事务所座右铭”，最最掷地有声的成功要诀，是“创业时千万别把自己当人”。<br />
<br />
&nbsp; &nbsp; 兹将其座右铭和“经验”恭录如下：<br />
<br />
&nbsp; &nbsp; 一、每日战战兢兢，每日如履薄冰，每日诚惶诚恐；<br />
<br />
&nbsp; &nbsp; 二、天有三宝：日、月、星；地有三宝：风、水、土；人有三宝精、气、神。<br />
<br />
&nbsp; &nbsp; 三、【 】楼高百尺，手可摘星辰；不敢高一语，恐惊天上人。<br />
<br />
&nbsp; &nbsp; 四、创业时千万别把自己当人。<br />
<br />
&nbsp; &nbsp; 五、伟大【是】琐碎堆出来的。<br />
<br />
&nbsp; &nbsp; 。。。。。。<br />
<br />
&nbsp; &nbsp; 恍惚自己老不成功，原来是炼不到“不拿自己当人”的境界，战战兢兢如履薄冰和诚惶诚恐，也不老能做得到。燕赵之光律师楼已经如此成功了，不知他们何以消受成功的乐趣――匍匐在“为虎作伥贪赃枉法”的法院旁门边角儿，把“千万别拿自己当人”的白旗举得高高，能否感动上帝难说，款曲个把法官，估计能做得到。<br />
<br />
&nbsp; &nbsp; 浦志强 2010年9月7日 北京</div>

							
							
							
							
