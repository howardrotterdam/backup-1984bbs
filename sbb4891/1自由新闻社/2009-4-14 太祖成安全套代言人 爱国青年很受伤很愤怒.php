																											<div id="postmessage_73931" class="t_msgfont">毛泽东成德国安全套广告主角 海外华人表气愤<br />
2009-04-14 来源: 环球时报(北京)<br />
<br />
核心提示：一家名为“Ads of the World”（世界广告网）网站的“全球广告展示”栏目中见到一份名为“Doc Morris药房：毛泽东”的广告，海外华人对此表示非常气愤，认为这则广告伤害了中国人民的感情，必须要道歉。<br />
<br />
德国一家广告商竟将中国已故领袖毛泽东作为安全套广告的主角，还和希特勒、本・拉登并列，以体现其“创意性”。该广告自今年3月问世以来，在德国引起广泛质疑，尤其令海外华人非常气愤。一名当地学者表示，这种为出位、出名，不惜抹黑中国的做法“令人难以接受”。<br />
<br />
最近，很多华人向《环球时报》记者反映说，他们在一家名为“Ads of the World”（世界广告网）的网站看到一张侮辱中国领袖的广告。记者在该网站的“全球广告展示”栏目中果然见到一份名为“Doc Morris药房：毛泽东”的广告。该广告借用中国领袖的形象，并加上希特勒与本・拉登，来为一家荷兰药店的安全套做广告。<br />
<br />
据《环球时报》记者了解，广告制作方Grey Worldwide公司为德国第二大广告公司，已有50年历史，在97个国家设有428家办事处，包括中国上海。《环球时报》记者13日致电该公司，但由于正值复活节，无人接听。<br />
<br />
Grey Worldwide公司不知是没预见到这样的广告的恶劣后果，还是成心要这样做。住在柏林的沈影女士气愤地向记者表示，这则广告伤害了中国人民的感情，必须要道歉。“Ads of the World”网站的读者评论也给广告打了低分。在以10分为满分的读者评分中，该广告只得4.5分。一名叫瑞皮的网友说，“把中国领袖和希特勒放在一起？这很可恶！”<br />
<br />
德国国际媒体研究所专家艾特伯斯在接受《环球时报》记者采访时表示，该广告的创意是“即使像毛泽东那样强大，我也不怕”，以此说明安全套的保险性。但这样做显然违反了做广告的原则，并伤害了中国人的感情。艾特伯斯说，把受中国人爱戴的中国领袖毛泽东与希特勒和拉登“混为一谈”，令人难以接受。Grey Worldwide公司显然是想借中国领袖出位、出名，甚至不惜“抹黑中国”。另据德《霍利特日报》报道，德意志银行已拒绝与Grey Worldwide公司合作，因为不满其创意。<br />
<br />
这已不是德国公司第一次利用中国领袖形象做广告了。一家德国保险公司曾用中国领袖作为电视广告的主角；一家广播电台还在领袖的眼睛上画上装饰；还有一些公司像Grey Worldwide公司一样，肆意篡改中国领袖的形象，以达到他们的宣传目的。2008年1月，法国雪铁龙公司在西班牙的一幅广告中篡改了中国领袖毛泽东的形象，该公司后来道歉。</div>

							
							
							
							
