													<h2>克林顿披露美轰炸中国大使馆是既定目标</h2>
						
						
																											<div id="postmessage_771881" class="t_msgfont">震撼内幕!克林顿曝光美轰炸中国大使馆是既定目标&nbsp;&nbsp;<br />
 文章来源: 环球网 于 2010-08-05 22:49:20 <br />
 <br />
　　1999年5月13日，在5枚导弹轰炸中国驻南斯拉夫大使馆后，由于美国国会的议员们强烈要求说明真相，克林顿总统来到国会发表了秘密讲话。<br />
<br />
<br />
<br />
　　“首先，我必须申明，我的以下讲话，将涉及我们国家的最高利益，当然也是最高机密。虽然在这个星球上，我们不用怕谁，但是，在当前这个阶段我们也不想制造任何不必要的麻烦。”“众所周知，冷战结束以来，就美国的综合国力而言，我们在全球不仅没有对手，而且完全有能力控制全球。”“我所要描述的新战略的目标将是从现在起，美国将成为人类的最后唯一的帝国。”“我们制定了详细的目标：第一步：北约东扩。”“诸位已经看到，在科索沃战事上，原来我们还对他（俄罗斯）心有余悸，现在，他几乎成了一丝不挂的乞丐。但是，我想诸位议员能够明确地了解，我们的东扩，不仅仅是让叶利钦出出洋相，我们东扩的目的是控制整个欧亚板块，在那里，在东南亚有一个我们头痛的地方，就是中国。”“总之，在这条路上虽然也有一点冒险性，但基本上没有可以阻挡我们的敌人。一个完全在美国领导下的21世纪很快就会到来。”（全场掌声）<br />
<br />
<br />
<br />
 <br />
<br />
<br />
　　接下来，克林顿针对议员们的提问，将美国的战略意图表露得清清楚楚。<br />
<br />
<br />
<br />
　　一议员：总统阁下，科索沃战事久拖不决，费用一增再增，到底还要打多久。<br />
<br />
<br />
<br />
　　克林顿：“纵观一部美国的发展和发达的历史，二次世界大战给我国经济带来高速发展已经足以证明，战争符合美国的利益。”<br />
<br />
<br />
<br />
　　一议员：科索沃是否可能成为下一个朝鲜或越南，让我们陷入战争的泥潭。<br />
<br />
<br />
<br />
　　克林顿：“海湾战争已证明了我们在战争中的伤亡几乎等于零，这就像玩一个电子游戏。所不同的是，他们是在用他们的生命和不多的、必须赖以生存的家当与我们赌博。”<br />
<br />
<br />
<br />
　　一议员：在科索沃战争中我们的一再失误，造成1500多名平民伤亡和中国驻南大使馆被炸，这是否证明，我们的战争机器已不再受控制或有别的原因。<br />
<br />
<br />
<br />
　　克林顿：我想在这里强调的是，所有的轰炸目标都是既定目标。同时，我想引用中国领导人的一句话：炸完军事目标、道路、桥梁，我们还能炸什么呢。但轰炸必须继续进行下去。再重申一遍：这符合美国的战略利益。<br />
<br />
<br />
<br />
　　一议员：毫无疑问，轰炸中国大使馆是一次精心策划的结果，刚才阁下也承认了这一点。可是我似乎看不出有什么符合美国利益的地方。<br />
<br />
<br />
<br />
　　克林顿：除了莫斯科，东扩――不仅仅指东欧，在更远一点的地方，有一个让我们更担心的国家，他同时也是一个核大国，那就是中国。他本来应该在10年以前就已分成7个国家，可是，至今仍似乎牢不可破。尽管在我们的各种打压下，他的发展仍然令人吃惊，而且等他自行内部肢解的可能性不大。出于一种考虑，应该让他沿着前苏联的老路走，即疯狂地军备，这样足以拖他下水。不远的将来，他将同样因经济崩溃而无力对我们说“不”，并且沦为国际乞丐。<br />
<br />
<br />
<br />
　　一议员：据说中国的核武器如果在美国本土有效爆炸，可以把美国毁灭若干次。采取这样一种危险的策略是不是慎重。<br />
<br />
<br />
<br />
　　克林顿：“在此之前，我们做了慎重的研究，分析了各种可能性。我们得出的结论是：首先，“误炸”会刺激中国人，但不致引发中国领导人启动核按钮。”<br />
<br />
<br />
<br />
　　“况且还有一个愚蠢的承诺（指的是中国不首先使用核武器，不对无核国家和地区使用核武器的政策）。其次，中国人现在最想做的事就是需要我们的科技。”“因此，他们在全球最不想得罪的就是美国。第三，有鉴于此，他们能做的一切也只有抗议而已。”“第四，即使做最坏的打算，他们动手，这里我又要再次引用朱F基的话，既然世界上最先进的武器全出自美国，我们还怕什么呢。”<br />
<br />
<br />
<br />
　　一议员：那么人权是否高于主权。<br />
<br />
<br />
<br />
　　克林顿：我觉得在我作完前面有关美国全球战略的谈话后再问这个问题，是政治上的幼稚。<br />
<br />
<br />
<br />
　　一议员：客观上发展中美关系符合两国的共同利益，可是轰炸使馆这一事件将会使中美关系严重倒退，况且已经造成大规模的反美情绪。毫无疑问，这也同样会影响到本国企业界的利益。总统阁下是如何认为的。<br />
<br />
<br />
<br />
　　克林顿：“在我们的全球战略中，这（中国）是一个可怕的障碍。”“但我可以预见，中国领导人比我们更渴望恢复中美关系。”“他们的几张关键的底牌也在我们手中，诸如台湾问题、西藏问题、新疆问题，等等。不管在什么时候，只要我们想做点文章，是不难找到话题的。”“要在欧亚板块全面达到我们的目的，仍然有些麻烦的地方诸如中国和印度等，不过我们的形势仍然有利。在东方，有日本和台湾，作为我们的永不沉没的航空母舰，对中国形成钳制的形势。”“全球将按我们的游戏规则进行，任何行动必须是在符合美国利益的前提之下。在这之后，我们只有一个敌人：外星人，如果他们存在，而且比我们强大的话。”（掌声雷动）</div>

							
							
							
							
