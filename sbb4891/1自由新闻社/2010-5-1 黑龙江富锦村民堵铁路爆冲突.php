&nbsp; &nbsp;&nbsp;&nbsp;富锦市政府集中完国有荒地之后，发现还不够协议规定的57万亩，于是又向周边的集体土地进行征收，这部分土地按照当时的市价给了农民补偿。<br />
但这个占用土地达57万亩的合作项目实施不到三年，东南亚金融危机爆发，韩方无力再投资，项目中止。<br />
后来，黑龙江省政府授权富锦市政府成立了富锦市农业开发公司，负责管理这57万亩土地中的37万亩。另外20万亩则由黑龙江省政府直接管理。<br />
距富锦县城东南46公里的长春岭村号称是富锦市最大的行政村，拥有2600多人口，土地面积达9万多亩。长春岭村的土地证上，清楚地写着，“土地使用总面积9.03933万亩，属于生产大队集体所有的土地为6.42862亩，属于生产队集体所有的土地为2.61071万亩。”细则里标明，“耕地的面积是2.3万多亩，荒地面积是近6.4万亩”。<br />
但长春岭村农民实际拥有的土地只有两万多亩，其余6万多亩土地现在都归富锦市农业开发公司所有。<br />
长春岭的农民要种地必须和公司签“承包”合同，一年一签，农民们是否在自己的土地上当“佃农”，农业开发公司在当“地主”。<br />
长春岭的农民多次组织上访对那6万多亩土地提出质疑，均未得到合理的答复，一些农民联合起来要强行收回土地受到富锦市政府的强力制止。<br />
