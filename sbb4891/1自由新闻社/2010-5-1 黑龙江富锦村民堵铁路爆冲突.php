国土局自认圈地有理 众心不服 <br />
<br />
国土局的工作人员表示，之所以认为土地证不合理是因为长春岭的土地面积太大，而且黑龙江省的有关专家也表示了质疑。<br />
按此说法，长春岭村的土地证在核发时就可能存在问题，土地证上的6万亩荒地应该属于国有，而不是集体所有。<br />
