<br />
佳木斯到前进镇4133次火车到富锦突然中断：<br />
<br />
/来自中华网社区 club.china.com/<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 在2010年04月28日下午15点11分，4133次火车开往前进镇途中19点17到达富锦时车上通知铁路中断，下车后火车站给所以旅客全额退票 。富锦火车站工作人员称是向阳川、长春岭农民上访没有得到解决，激愤的农民横卧铁轨，并将同三公路用数十台拖拉机堵死。据称去年有过一次但只拦截公路没有拦截铁路。富锦市政府没有给解决，是因为农民上访土地的事。因此同三公路中断2天，当地群众打死一名警察，重伤数人。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 在2010年04月30日凌晨，省武装部队紧急派来2000多名特警维和人员来到富锦，维持当地秩序并保护当地官员生命安全，富锦市中央大街和东方大厦已经被荷枪实弹的武警人员管制。<br />
<br />
