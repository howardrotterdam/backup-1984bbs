&nbsp; &nbsp; 富锦的干部手中握有多少土地，看看群众提供的这个名单就管窥一斑：<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 原富锦市委副书记葛其侠，占地近万亩，在大草岗5000亩，靠前锋农场5000亩，管理者是富锦市三元公司刘连庆。<br />
