<br />
富锦市政府跑马圈地6万亩 土地主人成“佃农”<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 地处我国东北边陲的黑龙江省富锦市是一个拥有大面积国有、集体荒地的县级市，其可耕种的土地面积在其所属的佳木斯市居第一位。本来这样优厚的自然条件给了以农业立县的富锦很大的发展空间，但近年来该县农民因为土地权属、使用纠纷多次令世人瞩目。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 其中引人注目的问题之一是富锦市借当年国家项目之名非法圈占一块面积达6万多亩的集体土地，14年来，土地的原主人只能租种原本属于自己的土地，实际上成了“佃农”。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 土地被占14年 农民成“佃农”<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 1994年，中韩两国政府签订协议，要在黑龙江省富锦市合作办农场，按照规划，需要土地57万亩。这部分土地绝大部分是富锦市辖区内的国有荒地，有少部分是属于集体所有的田地，县政府采取征用的办法获得。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 富锦市政府集中完国有荒地之后，发现还不够协议规定的57万亩，于是又向周边的集体土地进行征收，这部分土地按照当时的市价给了农民补偿。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 但这个占用土地达57万亩的合作项目实施不到三年，东南亚金融危机爆发，韩方无力再投资，项目中止。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 后来，黑龙江省政府授权富锦市政府成立了富锦市农业开发公司，负责管理这57万亩土地中的37万亩。另外20万亩则由黑龙江省政府直接管理。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 距富锦县城东南46公里的长春岭村号称是富锦市最大的行政村，拥有2600多人口，土地面积达9万多亩。长春岭村的土地证上，清楚地写着，“土地使用总面积9.03933万亩，属于生产大队集体所有的土地为6.42862亩，属于生产队集体所有的土地为2.61071万亩。”细则里标明，“耕地的面积是2.3万多亩，荒地面积是近6.4万亩”。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 但长春岭村农民实际拥有的土地只有两万多亩，其余6万多亩土地现在都归富锦市农业开发公司所有。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 长春岭的农民要种地必须和公司签“承包”合同，一年一签，农民们是否在自己的土地上当“佃农”，农业开发公司在当“地主”。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 长春岭的农民多次组织上访对那6万多亩土地提出质疑，均未得到合理的答复，一些农民联合起来要强行收回土地受到富锦市政府的强力制止。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 14年来，富锦市政府有关部门一直强调，在建立中韩合作农场时，政府只征用了长春岭村的1万亩土地，并给予了52万元补偿款。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 言外之意，即指长春岭农民索要的6万多亩土地是子虚乌有，伸手向富锦市农业开发公司要地是在打国有土地的主意。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 2007年，富锦市国土资源局的工作人员终于承认长春岭村讨要的那6万亩土地确实是被市政府占了，但它们不同意归还土地，因为国土局认为长春岭的土地证不合理。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 国土局自认圈地有理 众心不服 <br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 国土局的工作人员表示，之所以认为土地证不合理是因为长春岭的土地面积太大，而且黑龙江省的有关专家也表示了质疑。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 按此说法，长春岭村的土地证在核发时就可能存在问题，土地证上的6万亩荒地应该属于国有，而不是集体所有。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 但孙富运认为事情没这么简单，富锦市政府之所以隐瞒这6万多亩土地的情况，是因为牵涉到经济利益。他认为，这块大面积的土地是政府集中土地时顺手牵羊收走的，对村民说是征用，对外却只承认1万亩，其中5万亩的落差即是可观的经济利益。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 针对富锦官员提出的关于对长春岭村土地证的质疑，有关法律专家表示，只要没有被国家废止，这样的土地证就是合法有效的，如果政府对其有怀疑，应该走正当的程序，不能想当然地认为它错了。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 《中华人民共和国土地管理法》第十二条规定：依法改变土地权属和用途的，应当办理土地变更登记手续。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 但长春岭村村委会并没有参加任何变更那6万多亩土地权属的活动或讨论，也没有任何证明那块土地权属变更的文件和资料。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 按照《中华人民共和国土地管理法》第十六条规定，土地权属争议的处理应该遵循“单位之间的争议，由县级以上人民政府处理”，“在土地所有权和使用权争议解决前，任何一方不得改变土地利用现状”的原则。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 事实上，富锦市政府在收走长春岭村那6万多亩土地时，根本就没有给土地的主人协商的余地，行为无异于强抢。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 和一些地方政府先斩后奏征用农村集体土地相比，富锦市政府圈占长春岭的土地可以说是只斩不奏，堂而皇之地占用14年之久，不补走法律程序，也不做善后事宜，心安理得的让人难以理解，似乎要将违法行为进行到底。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 富锦市民间一直存在这样一种说法，富锦市农业开发公司一直在为当地官员谋取私利。农业开发公司的37万亩土地中只有少部分是直接发包给农民的，大部分土地都是先包给中间人，然后由中间人再转包给种地农民。这些中间人都是当地官员的亲戚，官员们通过这些“白手套”坐收暴利。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 现在，长春岭村的农民还在为属于自己的土地奔走呼告，希望国家有关部门能维护法律的尊严。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 最牛逼的大队书记<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 当地村民每家每户都必须出一个男壮丁，出来按血手印，也就是生死状。如果被警察打死或者残疾，算你点背，你家老小有全体村民抚养，你家地全村人帮你种植~！！长春岭的书记认可国家判我刑我也要告 也要闹事！也有不愿惹事的村民不想参加，书记领着全村人去扒他们家的房子，扒到你按手印为止！全村两千多村民 男女老幼齐上阵！警察基本上全部受伤。打仗的时候非常有纪律性，第一排拿洋叉挡住 第二排拿砖头往警察那里砸！ 人家不和你政府谈 来了就是打！心非常齐。啥时候中央来管这事的了啥时候拉到！砖头的数量整整拆了一堵墙！<br />
<br />
