																											<div id="postmessage_741269" class="t_msgfont">上大教授朱学勤称媒体不应对学术问题充判官<br />
2010年07月15日04:46&nbsp;&nbsp;人民网-人民日报 <br />
<br />
事件回放<br />
<br />
　　网友Isaiah在网上发表6篇题为《朱学勤：学术界的又一个 “汪晖”？》的文章，通过比对指出，上海大学教授、知名学者朱学勤1992年的博士论文《道德理想国的覆灭》存在抄袭嫌疑，并声明“不是为了打倒谁，写文章是为了说时代和学术规范的变迁问题。”该文随即在一些学术网站被转载，并经媒体报道后变成公众议论的话题。<br />
<br />
　　7月13日，朱学勤向上海大学和复旦大学递交启动学术调查程序的申请。<br />
<br />
　　当晚，朱学勤教授就此事接受了本报记者专访。<br />
<br />
　　“希望面对的是学术机构的调查”<br />
<br />
　　记者：抄袭指控给你带来多大的困扰和影响？从一开始你就说不会沉默不语，为什么会选择这种态度，而且主动申请启动学术调查？<br />
<br />
　　朱学勤：从来没有被媒体这样密集轰炸，形成很大的冲击波。我希望能以冷静平和的方式证明自己的清白，不想被媒体炒作或网络意见左右，形成混战。<br />
<br />
　　学术规范问题应由权威学术机构甄别，而不能由网络、大众媒体做判断。我希望自己面对的是学术机构的调查。<br />
<br />
　　学术抄袭事件近年来之所以愈演愈烈，在我看来，不少与当事人及权威学术机构没有及时采取适当手段作出公开回应有关。许多高校、学术机构都已建立了学术委员会，调查学术规范问题、惩处“学奸”、维护教师及学校声誉、保持学术环境和学术尊严，正是这些机构的职责承担。我被指控，起于网络、传媒，但很想试一试，能否从传媒走向学术？虽“原告缺席”，作为“被告”，我希望走向学术“法庭”，要求“开庭审判”。维护学术尊严，应该是做，而不是说。<br />
<br />
　　现在复旦大学将启动学术调查，我只需配合调查，随叫随到，平心静气地等待他们做出仲裁。<br />
<br />
　　“我做好了各种心理准备”<br />
<br />
　　记者：通常，大学学术委员会都是接到对学术违规的实名举报材料，才会受理调查。受指控者主动找上门要求接受调查、“还我清白”的案例极少。我们维护学术研究风气的制度是否应该进一步强化，比如不需要实名举报或当事人申请，还可以有学术机构发起的“公诉”机制？<br />
<br />
　　朱学勤：复旦大学学术规范委员会成立于2005年，我是第一个主动提出调查申请的当事人。递交申请报告、提供有关资料，实际上是替检举方完成的规范举报程序。我愿意“以身试法”，点把火烧一烧自己，期望这样的做法有利于维护在大众心目中已摇摇欲坠的学术尊严。<br />
<br />
　　近几年，国内很多高校相继建立学术委员会，教育部还成立了高校学风建设委员会，受理直属高校的学风问题举报并组织对重大学风问题进行调查核实，提出处理建议，对学术道德和学风建设问题不可谓不重视。如此高规格的学术问题监督和纠错机构，在国外也并不多见。据我所知，美国高校、科研机构的学术规范监督和惩戒系统具有高度自治，通常也需要有举报，再由学术委员会匿名调查操作。严格的学术规范与惩戒系统，与教授们所能享受的社会尊重相对等，是人人必须遵守的游戏规则。<br />
<br />
　　记者：主动要求接受学术调查，是因为对自己的博士论文在学术规范方面十分自信吗？是否想过，调查也完全可能得出“抄袭”结论？<br />
<br />
　　朱学勤：当然自信。我做了自检，发现Isaiah的指控并无根据。对很多只看过该文及媒体报道跟着起哄的人，我的建议是：读书吧。在学术问题上，大众传媒应该更多关注当事人和批评者双方的行为反应方式、仲裁机构运行方式等，而不是对学术问题本身充任审判官。<br />
<br />
　　既然将问题交由学术机构调查仲裁，当然也会做好另一种心理准备。一旦出现不利于我的结论，坦然接受。用不着母校通知，自动交出当年得来不易的博士学位证书，把它留在母校校史室里，警戒后人。随后，上海大学会重新考量我的教授、博导头衔，因为它们是依据我的博士学位才获得的职业资格。国内至今很少有因为学术规范问题受惩戒而自动辞职者，自尊心会驱使我做这样的选择，我受不了同行尤其是学生印在我背脊的白眼，留在这里“混”。<br />
<br />
　　“从我开始，为学术界积累一个案例”<br />
<br />
　　记者：有人担心，在现有社会环境下，不论是母校还是工作单位，对自己的学生、有学术知名度的教师提起学术调查，能否做到公正？<br />
<br />
　　朱学勤：学术机构调查确实会存在某些弊端。但相比之下，它还是我们最好的选择，必须尊重。身为上海大学学术委员会成员，此前我只参与过原则问题的讨论，没有参与过具体案例的甄别。但把问题交给了仲裁者后，我心里很轻松。我只要配合调查，静心等待结论。<br />
<br />
　　记者：近30年来，我国学术界在论文引文、注释等规范方面经历了从模糊到严格、从粗疏到细致的过程。以今天的标准回溯当年的论文成果，或许会有“抄袭”、“剽窃”的痕迹？<br />
<br />
　　朱学勤：形式上看，现在学术界对于引文、格式、核心期刊、知识产权等等的确比以前更重视更详细。但在学术研究的精神实质、对学术的敬畏及学风方面，我更怀念和敬重上世纪80年代，那是人们求知、求真的热情井喷式爆发。学术风气是不可能与社会风气脱节，孤立存在的。90年代之后，学术披上了华美的衣衫，没魂了，失魂落魄。<br />
<br />
　　记者：连续出现知名学者、名人抄袭或造假事件，有人说是“学派之争”，你怎么看？<br />
<br />
　　朱学勤：被指控“剽窃、抄袭”，当然要反驳。但要面对“原告”指出的基本事实说话，而不是离开事实，放纵文学想象。并不是“阴谋论”需要勇气，恰恰相反，朴素地接受这个世界是朴素的，好事、坏事都可能是朴素地发生的，坚持这样的思维，才需要勇气。<br />
<br />
　　学术界经常说西方学术规范如何严格，如果遇到类似事件，真能学会按照学术规则和程序来处置，而不是一味抱怨风气、指责他人，那我们就收获了一个有利于学术发展的健康案例。如同英美法系采用的判案体制，靠的就是一件件案例的示范累积。在端正学术风气、恢复学术生活的社会尊严这件事上，靠自上而下的强制、号召、命令或自下而上的“革命”，作用都不大。<br />
<br />
　　我刚听过一句德国谚语：“站在哪里就从哪里挖下去”，它始于德国民间对当年法西斯主义的自我反思。既然我“被”卷入了抄袭风波，我应该从自己站着的地方开始，为学术界积累一个案例。即使个人失败，这个案例不会失败。如果因此而站立，那么我个人也值了。</div>

							
							
							
							
