																											<div id="postmessage_736622" class="t_msgfont">朱学勤回应涉嫌抄袭：不会是第二个汪晖 <br />
<br />
上海大学教授朱学勤早年的博士论文《道德理想国的覆灭》被网友Isaiah指称存在“抄袭嫌疑”后，迅速成为新的学术关注事件。 <br />
<br />
事件的核心人物朱学勤今晚8点接受《第一财经日报》电话采访时称，自己已经接到很多媒体的电话，正在写正式的回应文章逐条反驳，将不日发表。朱学勤在电话里语气平静，他说：“不会像汪晖那样一言不发，不会成为第二个汪晖。”<br />
<br />
“非常希望启动调查机制”<br />
<br />
朱学勤表示，在回应文章里，他会指出，其一，Isaiah在网络和报纸上，一直不敢公开真实姓名，这是对学术界以及对Isaiah自己的不尊重，应该有胆量对其言论负责；其二，Isaiah很多说法属于不实之词，不能成立，他会逐条加以反驳。<br />
<br />
21世纪教育研究院副院长熊丙奇也关注了“朱学勤事件”，他今日对本报称，不论是对于汪晖还是朱学勤事件，他们是否涉嫌抄袭自己并没有专业能力判断，但是解决学术争端的基本制度应该是一致的。<br />
<br />
熊丙奇表示，认定汪晖是否抄袭，应该由授予汪晖博士学位的中国社会科学院学术委员会进行独立调查，然后进行处理，不管有多少人“挺汪”还是“倒汪”，最终解决纷争的根本途径还是取决于独立专家组的调查、听证，然后给予处理。<br />
<br />
而同样，授予朱学勤博士学位的复旦大学学术委员会也应该启动类似的调查，这是还被质疑者一个清白的途径。“这对朱学勤本人和学校的声誉都很重要。国外大学遇到检举、质疑，依靠的就是这样的基本模式”。熊丙奇说。<br />
<br />
学者易中天7月9日也在《新京报》上撰文《汪晖教授请勿坐失良机》指出，没有权威机构的结论，你就永远是“涉嫌抄袭者”。<br />
<br />
对此，朱学勤坦言，非常希望复旦大学启动调查机制。<br />
<br />
另一当事人的说法<br />
<br />
Isaiah通过将《阳光与闪电》与《姊妹革命：美国革命与法国革命启示录》比对，Isaiah指出，《阳光与闪电》一文，“抄袭了Dunn的研究成果”（苏珊&#8226;邓恩，Susan Dunn，美国威廉姆斯大学法国文学与思想史教授），“将间接引用伪装成直接引用，并抄袭了杨小刚等人的译文”。<br />
<br />
对此，《姊妹革命：美国革命与法国革命启示录》一书译者杨小刚表示，关于朱学勤涉嫌抄袭事件，涉及到的一些事实和背景，可能Isaiah先生不太了解，所以造成抄袭的错觉。杨小刚现为第一财经员工。<br />
<br />
杨小刚称，其在上海大学读硕士时，学校需要一些科研成果，包括翻译学术书籍或者在核心期刊发表文章才能毕业。朱学勤老师便推荐了Dunn这本书要他翻译。上海文艺出版社觉得此书还有点价值，所以要求出版，并请朱学勤写了一篇序言，这序言就是《阳光与闪电》一文，但后来由于种种原因该序言没有随书一起出版。<br />
<br />
“或许因为朱学勤觉得此书的一些思想跟他《道德理想国的覆灭》一书可以互相补充，就在这书再版时把这篇序言收录进去作为附件”。杨小刚说，如果这篇文章作为Dunn译本序言，引用一些文章内容，并无不妥，而且是非常必要的。当它作为该书的序言时，如果还在文章中注明出处，就多此一举了。何况，朱学勤的文章在开头就介绍了Dunn的这本书。<br />
<br />
至于是间接引语还是直接引语，杨小刚认为，直接引语要求原文原话毫无偏差地引用，但是如果作者出于不能完全保证忠于原作者意思的情况下，间接引语或许更妥当。<br />
<br />
学术何时自治<br />
<br />
最近涉嫌抄袭、造假的事件层出不穷。如何建立防止学术腐败的有效机制？熊丙奇开的药方是建立学术共同体，学术自治，摆脱行政的影响，学者贡献不由官方认定，不由媒体认定，而是遵从学术共同体认定的学术标准和规范，不看所谓的影响和江湖地位。<br />
<br />
复旦大学哲学系教授、博士生导师王德峰今日对本报称，自己并不了解朱学勤事件，对事件本身无法置评。但是，他也表示，抄袭成为学术界的病症，不能因为学术体制的弊端为抄袭辩护，有些学者遗忘了对社会的责任，谋求体制下的利益，而“学术良知是知识分子的最后立足点，学者应该安贫乐道，良知不改”。<br />
<br />
“学术乃天下之公器，不公开姓名的指控，非常不严肃，可以称得上‘懦夫’，此前，不管是指控汪晖还是为汪晖辩护的人，都是公开姓名的。”朱学勤说，真相总有大白天下的那一天，坏事会变成好事。他还希望读者不要看一面之词，可以去看书本身。</div>

							
							
							
							
