													<h2>上海大学教授朱学勤博士论文被指涉嫌抄袭</h2>
						
						
																											<div id="postmessage_733017" class="t_msgfont"><img src="http://img1.gtimg.com/news/pics/hv1/81/27/571/37136241.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
朱学勤博士论文《道德理想国的覆灭》<br />
<br />
<br />
<img src="http://img1.gtimg.com/news/pics/hv1/80/27/571/37136240.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
朱学勤<br />
<br />
　　不愿亮出真实身份的网友 Isaiah是看着汪晖、朱学勤(左)、王铭铭等所著的“问题书籍”入门的，这是一些人无法回避的阅读历史。<br />
<br />
　　“朱学勤：学术界的又一个 ‘汪晖’？”这是网友Isaiah在网上发表的系列长文题目，在6篇文章里，Isaiah通过比对指出，上海大学教授、知名学者朱学勤早年的博士论文《道德理想国的覆灭》存在抄袭嫌疑。仅仅几日，该文已经被各大学术网站迅速转载。而朱学勤昨天表示，在恰当的时候会作正式详细回应，特别是希望Isaiah能亮出真实身份。早报记者 石剑峰<br />
<br />
　　学术不规范？<br />
<br />
　　“将间接引用伪装成直接引用”<br />
<br />
　　Isaiah指出《道德理想国的覆灭》一书存在抄袭嫌疑，这马上会让人联系到至今依然被热议的“汪晖事件”，中央电视台甚至在前几日也就此事件作专题节目。而在Isaiah和诸多阅读了Isaiah文章的网友看来，《道德理想国的覆灭》存在的学术问题和汪晖《反抗绝望》有诸多相似之处，Isaiah本人也承认了这一点。但 Isaiah在系列文章的最后表示，“我们这一代做学术或思想的人，都是看着汪晖、朱学勤、王铭铭……这种问题书籍入门的，这不仅是别人的耻辱，也是我们的历史。如果我们今天走得更远了点，那也是在这些问题书籍所开拓的道路上。我们没有受到更良好的教育，是我们的遗憾，但是如果把这些问题书籍都清除掉，我们恐怕比现在还差得远。这是事实。”<br />
<br />
　　Isaiah是某大学在读博士，暂时还不愿公开自己的真实身份，在接受早报记者专访时，Isaiah透露，自己指出朱学勤涉嫌抄袭的系列文章是从本周三陆续开始写的，一开始并没有完整的计划。其中，比较有说服力的部分是第二篇、第四篇和完结篇，第四篇和完结篇主要针对朱学勤当年的博士论文《道德理想国的覆灭》。<br />
<br />
　　Isaiah在第二篇中主要指出朱学勤《阳光与闪电》存在抄袭嫌疑。通过将《阳光与闪电》与《姊妹革命：美国革命与法国革命启示录》比对，Isaiah指出，《阳光与闪电》一文，“抄袭了Dunn的研究成果”， “抄袭了本书译者杨小刚的译文”， “将间接引用伪装成直接引用，并抄袭了杨小刚等人的译文”。<br />
<br />
　　朱学勤昨天在电话中向早报记者表示，《阳光与闪电》原先是《姊妹革命》一书的序言，后来为了保证该书出版，序言从书稿中撤了下来，后单独收入在《道德理想国的覆灭》一书中。朱学勤认为，《阳光与闪电》一文本来就是序言，是顺着该书内容写的，当然要大量引用书中的译文和内容。而Isaiah也提到，他也知道该文原为《姊妹革命》序言，但他同时认为朱学勤除了在文章开头部分提到了《姊妹革命》一书，“再无提过该书或该作者，没有任何地方坦承这是Dunn的观点，也从来没有在脚注中引用过。因此，读者有充分理由认为，朱学勤可能只是从该书中受到启发，而文中的资料和观点都是朱本人的。” Isaiah同时认为，《姊妹革命》出版于2003年2月，而 “朱学勤的后记是在2003年7月写的，有充分的时间看到出版了的该书”，也就是说在该文出版前完全可以根据已经出版了的书籍信息进行引用来源的注明。<br />
<br />
　　涉嫌抄袭？<br />
<br />
　　大量使用西方学者的研究和引用，却并未加以注明<br />
<br />
　　在Isaiah系列文章中，第四篇和完结篇通过比对美国学者Carol Blum的著作《卢梭与德性共和国》，指出朱学勤的博士论文《道德理想国的覆灭》存在抄袭嫌疑。“通过对朱学勤先生赖以成名的博士论文《道德理想国的覆灭》与美国学者Carol Blum的著作《卢梭与德性共和国》部分内容的对勘，(指出)其在未经注明的情况下，大量或明或暗，或隐或显地抄袭、盗用后者的事实。”Isaiah在文章中说。仅比对《道德理想国的覆灭》一书几十页，Isaiah就指出十多处《道德理想国的覆灭》与《卢梭与德性共和国》相似的地方。Isaiah首先认为，“朱学勤先生在博士论文中对卢梭等人的原著缺乏第一手的熟悉，绝大多数标明法语原著，引文可能都并非出于原著，而大量利用了Blum等西方学者的研究和引用，却并未加以注明。”此外，Isaiah举出多个例子证明，《道德理想国的覆灭》一书直接使用了《卢梭与德性共和国》的内容，但未详细标明是引用还是自己的成果。“朱书172-182页，基本是拷贝Blum书135-142页。朱书 298-301页，基本是缩写Blum书260-277页。”<br />
<br />
　　然后，Isaiah通过比对和研究继续认为：“从书名到中心思想，从结构到具体内容，从材料到语言，朱书都大量借鉴，沿袭了Blum的著作。Blum书的基本框架，即是对卢梭‘德性’思想的解析(第1-6章)以及对其在法国大革命中影响和作用的考察(第8-14章)，这两个部分又以卢梭死后，大革命之前其形象和地位的变迁为连接(第7章)。这和朱书的三部分主体结构恰好相对应：第1-4章讲卢梭思想，第5章过渡，第6-8章讲法国大革命。Blum的书，专门讲德性概念，线索比较明晰，朱书则一会道德理想，一会先验理念，一会价值理性等纠缠不清。但中心思想都是一致的，即是讨论何以一种崇高的道德观念会演变成恐怖的意识形态的缘故。其中一些关键的概念，如‘语言转换’等也有相似点。”<br />
<br />
　　在昨天简短的电话中，朱学勤表示，他在《道德理想国的覆灭》一书里已经说明，Blum书是其参考文献，而且在书中也给出了比较详细的注释。不过朱学勤同时表示，他会在适当的时候作出正式的回应，特别是Isaiah能表明真实身份之后，会和他做学术上的探讨。<br />
<br />
　　早报对话Isaiah<br />
<br />
　　“时代和学术规范的变迁问题”<br />
<br />
　　早报：怎么会想到要写《朱学勤――学术界的又一个“汪晖”》？跟“汪晖事件”有多大关系？<br />
<br />
　　Isaiah：写这篇文章就是因为“汪晖事件”引起的。我和朋友也经常对当时的学风、学术规范等论题做了比较激烈的讨论，然后我就想找几本其他的著作来比较一下。朱学勤的书，我以前大致看过，内容相对熟悉，当然找来看看比较方便。<br />
<br />
　　早报：写这几篇文章大概花了多长时间？<br />
<br />
　　Isaiah：大概断断续续从周三晚上写到周五早上，满打满算也就是一天。我写好一部分就立刻贴出，绝没有什么事先策划之类说法。所以你看，我写的第一篇其实是有问题的，因为那个时候我还没有找到《姊妹革命》一书，误以为有问题的译文都是朱学勤自己翻译的，实际上后来发现都是出自该译著。如果我有深思熟虑，这部分肯定会删掉或者改写。但是后来发现，朱学勤的问题确实比我想的要严重一些。<br />
<br />
　　早报：在你看来，朱学勤在《道德理想国的覆灭》中的许多问题是否与汪晖在《告别绝望》中的问题有相似之处？<br />
<br />
　　Isaiah：朱学勤和汪晖，二者在一些规范问题上有类似问题，比如长篇大论地引用其他著作，并不完全隐瞒，但是也不一一仔细注明。又如一些注脚将间接引用写成直接引用等。这样从今天的标准来看就是抄袭，但是这种标准对当时的人来说并不公平。实际上这二十多年，我们的学术工作很大的力气就是引进西学，主要起个传声筒的作用。当然确实有完全不学无术，借此浑水摸鱼的，这些要具体分析。我觉得朱学勤应该不属于此列，至少在当时来说，他是一个真诚的思想者，写了一些言之有物的东西。<br />
<br />
　　早报：你作为一名年轻学者，怎么看汪晖、朱学勤，可能还有其他学者在早期著作中的一些学术规范上的问题？<br />
<br />
　　Isaiah：我文章的初衷是故意反话正说，不是为了打倒谁，写文章是为了说时代和学术规范的变迁问题。总的来说，我认为学界的态度应该是同情、理解。对这类事情，如果要批判的话，最好不要针对个人，这是历史，也是我们自身的一部分。要完全划清界限是不可能的。(东方早报)</div>

							
							
							
							
