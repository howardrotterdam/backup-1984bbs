																											<div id="postmessage_757989" class="t_msgfont">“将军不是太阳” 北韩出现反体制传单<br />
<br />
朝鲜日报驻东京特派记者 辛贞录 （2010.07.24 11:33）<br />
<br />
日本《朝日新闻》23日发自中国沈阳的报道称，今年6月下旬，在北韩咸镜北道会宁地区出现了主张“推翻体制”的传单。《朝日新闻》报道说，北韩公安当局于6月24日至25日没收了数十张传单，并将此次事件定性为“威胁到国家安全的事件”，展开了查找相关人士的工作。<br />
<br />
该报称，在传单上还写有“将军并不是21世纪的太阳，带给我们的只有黑暗”和“终止金正日时代，我们需要的是温饱”等内容。<br />
<br />
《朝日新闻》报道说，北韩当局认为，考虑到这些传单的内容和散发情况，不像是韩国军方或市民团体通过气球散发的传单，而像是北韩内部组织的行为。因此，北韩当局在烧毁没收传单的同时，正在动员所有治安部队调查是否还有人私藏传单。<br />
<br />
该报称：“从此可以看出，北韩内部对金正日十分不满。”此外，专门报道北韩相关新闻的网络新闻网站“Daily NK”也报道称，今年6月下旬，在咸镜北道清津地区发现了大量写有反对金正日体制内容的面值5000朝元的新版纸币。在纸币上还写有“亡命救国行动队”的组织名称，这些纸币主要散布在清津金日成铜像周边。<br />
<br />
