													<h2>政治体制改革:未来特区新使命</h2>
						
						
																											<div id="postmessage_817728" class="t_msgfont"><img src="http://epaper.nfdaily.cn/res/1/1/2010-09/06/02/res01_attpic_brief.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　话题缘起上月,在中国经济特区成立30周年之际,国务院总理温家宝考察了深圳,他在讲话中强调:“不仅要推进经济体制改革,还要推进政治体制改革。没有政治体制改革的保障,经济体制改革的成果就会得而复失,现代化建设的目标就不可能实现。”他还指出,我们站在一个新的伟大的历史起点上,要继续解放思想,大胆探索,不能停滞,更不能倒退。停滞和倒退最终只会是死路一条。在特区建立30年的历史节点上,作为各项改革的先锋和试验田,特区也应该是政治体制改革的先行者。那么,当前特区推进政治体制改革的着力点在哪里?本专栏特约专家建言。<br />
<br />
深化改革开放必须坚持市场化和民主化方向<br />
<br />
<br />
　　中国的改革只能走经济市场化与政治民主化兼容的道路。只有通过政治民主化才能最终实现经济市场化,才能使社会和文化生产力得到极大的解放和发展<br />
<br />
　　□钟坚<br />
<br />
　　<br />
<br />
　　牢固树立没有民主就没有社会主义的观念,加快推进政治体制改革<br />
<br />
　　邓小平曾反复讲过,“没有民主就没有社会主义”。胡锦涛总书记在十七大报告中也明确指出:“人民民主是社会主义的生命”。民主是社会主义的本质要求,没有民主就没有社会主义,就没有社会主义现代化。我们必须进一步深化对于民主的认识,将民主视为社会主义的生命线,树立社会主义民主法治、自由平等、公平正义理念。贫穷不是社会主义,没有民主更不是社会主义。我们更不能把民主的诸多内涵和形式与资本主义划等号。如果要与资本主义作比较的话,社会主义应该比资本主义更民主,在民主政治建设上比资本主义做得更好。同样,深圳要建设现代化国际性先进城市和中国特色社会主义示范市,没有民主政治作基础,最终也是不可能实现的。随着深圳经济体制的变革和经济的发展,政治发展的任务显得越来越迫切,也越来越重要。今天改革中出现的“放权”与“收权”的周期反复,行政体制紊乱造成的政策实施扭曲变形,以及垄断者的垄断和既得利益者的讨价还价,经济增长方式转变的步履蹒跚,都是政治体制改革滞后的副产品。一般来说,政治利益刚性强于经济利益刚性,在传统的政治利益与经济利益并存的情况下,只有突破政治利益的刚性,才能实现经济利益关系的调整,才能实现社会公平正义。邓小平曾经反复强调:“现在经济体制改革每前进一步,都深深感到政治体制改革的必要性”。“只搞经济体制改革,不搞政治体制改革,经济体制改革也搞不通”。所以,中国的改革只能走经济市场化与政治民主化兼容的道路。只有通过政治民主化才能最终实现经济市场化,才能使社会和文化生产力得到极大的解放和发展。<br />
<br />
　　把维护社会公平正义作为政治体制改革的出发点和归宿<br />
<br />
　　如果社会主义比资本主义更具有优越性的话,那就是在发展社会生产力的基础上比资本主义更加注重社会公平和正义。正义是社会主义制度的首要价值。实现公平正义是社会主义的本质要求。邓小平反复强调:我们坚持走社会主义道路,根本目标是实现共同富裕。”“社会主义原则,第一是发展生产,第二是共同致富。”“社会主义最大的优越性就是共同富裕,这是体现社会主义本质的一个东西。”“社会主义的目的就是要全国人民共同富裕,不是两极分化。如果我们的政策导致两极分化,我们就失败了”。我们必须继续促进发展的同时,把维护社会公平放到更为突出的位置,要让发展成果惠及全体人民。而要真正做到社会公平正义,就必须有现代民主政治来保障。所以,我们必须加快推进政治体制改革和民主政治建设,使政府在维护市场秩序和社会公平方面真正负起主要责任。<br />
<br />
　　政治体制改革和民主政治建设必须解放思想,突破所谓姓“资”姓“社”的新束缚<br />
<br />
　　我们深化改革的任务就在于,打破过去的旧体制,建立和完善以效率和公平两大目标相适应的新的经济社会运行机制,走出一条公平与效率兼得、稳定与激励并举的现实之路。<br />
<br />
　　第一,继续坚持经济市场化改革,实现发展模式由政府主导向市场主导转变。市场追求效率,政府保证公平。如何进一步培植和维护市场,最大限度地发挥好社会、私人和市场的作用,是深圳下一阶段体制改革的着眼点和重点。深圳未来必须根据科学发展观的要求,继续坚持市场化改革的方向,实现由政府主导向市场主导转变,更多地通过提高资源使用效率以及技术进步来促进经济的可持续发展。没有实现发展模式从政府主导向市场主导的转变,就不可能实现经济增长方式的根本转变。<br />
<br />
　　第二,坚持政治民主化改革,实现政治权力运作的民主化、程序化和法制化。民主是一个重要的合作机制,强调求同存异,而革命是你死我活。民主是一个重要的法制机制。民主必须事先通过法制而明确各自的游戏规则。政治体制改革的关键和牛鼻子,就是选举,就是看你的长官是不是由全体选民选举产生。同时,要建立起权力的制衡机制。政治体制改革,不闯选举关,将难以推进。民主不是天生就会,需要一个学习过程,如果什么事情都要等到条件都成熟后才搞,其实什么也干不成,也永远不会有成熟的一天。政治体制改革和民主政治建设,必须解放思想,突破所谓姓“资”姓“社”的新束缚,借鉴人类政治发展和民主建设的有益成果。<br />
<br />
　　第三,积极推进社会改革,大力培育社会中间力量,建立起市民社会。市民社会是一个国家或政治共同体内的一种介于“国家”和“个人”之间的广阔领域,它由相对独立的各种组织和团体构成,它是国家权力体制外自发形成的一种自治社会,是衡量一个社会组织化、制度化的基本标志,具有独立性、制度性的特点。没有一个完善和强大的市民社会的存在,所谓民主政治也是不稳定的。<br />
<br />
　　第四,加强法制建设,维护法制的尊严,建设起一个法制社会。<br />
<br />
　　作者系深圳大学中国经济特区研究中心主任、教授、博士生导师<br />
<br />
以政治体制改革为核心带动其他各项改革<br />
<br />
<br />
　　在党内民主制度建设方面先行先试:一是民主选举、“差额公推直选”扩大到镇、街党委会一级。二是依法治党、健全党的集体领导。三是推行包括党员在内的政府官员财产申报制度。四是党的纪律检查委员会实行垂直领导并独立运作<br />
<br />
　　□王利文<br />
<br />
　　<br />
<br />
　　广东经济特区已走过30年的历程,特区人敢为人先,艰苦创业,开拓进取,勇于创新,在经济体制改革和经济发展上创造出举世瞩目的辉煌成就,作出了巨大的贡献。目前,中国经济体制改革已经遭遇到来自政治体制的束缚,政治体制改革是绕不过去的一道坎,推行政治体制改革已经刻不容缓。党的十七大强调要进行政治体制改革,我们应当从国际社会及自身的改革中吸收营养,大力推进政治体制改革,大力推进执政方式转变。<br />
<br />
　　在全国全方位开放和全面推进经济体制改革,在经济体制改革课题谁都可能做和可以做好的当下,特区的使命和任务应作战略性调整,未来特区在政治体制改革方面应开始大胆试点,切实推进。应赋予特区新的历史使命:以政治体制改革为核心,带动其他各项改革―――包括深化经济体制改革、社会体制改革等等。<br />
<br />
　　党内民主建设关键在于建立有效的民主选举机制、权力监督制衡机制<br />
<br />
　　过去的政治体制改革经验表明,在推行党内民主制度建设中,关键在于建立真正的民主选举制度、集体领导制度和有效的监督制衡机制。如果干部的选拔采取党内一级级民主选举产生,或通过党代会集体讨论、差额选举产生,最后由上级依据规定任命,那么德才兼备、不唯上只唯实的领导干部才能脱颖而出,才能杜绝官场陋习。在对党的领导干部的监督上,目前各地建立了党的纪律检查委员会,是在同级党委领导下工作,但是严重缺乏对一把手的监督。因此,在党内民主制度建设方面,建议特区先行先试:一是民主选举、“差额公推直选”从目前的党支部一级扩大到镇、街党委会一级,并逐渐推进到区一级和市一级,实行党代表大会常任制。二是依法治党、健全党的集体领导。通过引入重大事项集体讨论与票决制度和代表质询制度,切实实施引咎辞职等制度,加强党的集体领导和集体监督,防止权力过度集中,主要领导“一言堂”、“一锤定音”的现象发生；三是推行包括党员在内的政府官员财产申报制度,并且在一定范围内公开,辅之以舆论监督,推动反腐败斗争深入进行。四是党的纪律检查委员会学习香港廉政公署运作方法,独立开展工作,不受同级党委领导,或由中央纪律检查委员会垂直领导并独立运作。<br />
<br />
　　改革人大代表的选举办法,提高人大代表的议政能力,建立健全人大的权力监督制度<br />
<br />
　　改革开放以来,尽管中国的人大制度已经取得长足的进步,但是距离法定的民意机关、权力监督机关仍然有相当距离。主要表现在:首先,人大代表选举依然实行间接选举制度,而间接选举、等额当选制度也使得人大代表候选人与民众之间缺乏沟通,人大代表高高在上,不了解民间疾苦,其民意代表的功能在打折扣。更为重要的是,这种间接选举制度产生的人大代表无形中具有“三多”现象―――官员代表多,照顾性质多,荣誉性质多。其次,代表参政议政能力严重不足,人大对政府的监督手段缺乏。在人大的监督制度上,尽管目前也出现了政府领导干部向人大述职制度,但是总体而言,人大代表对政府的监督手段和监督效果依然有限。要改变这种现象,就必须改变人大代表的产生制度,推行人大代表直接选举、差额当选办法；要改变人大“橡皮图章”形象,就必须赋予人大相应的权力,丰富人大的监督手段,强化人大的权威。<br />
<br />
　　建议:一是在特区试行“差额公推直选”制。从改革人大代表提名方式和代表数量入手,进一步优化人大代表的产生方式和职业结构。基层代表产生可以尝试调整为多渠道民主提名；增加基层代表,减少领导干部代表比例；进一步优化代表职业结构,适当增加非本市户籍人口的人大代表比例；选区逐步实行社区、行业协会、功能团体、事业单位混合划分；在候选人提名上允许行业协会、多功能团体依照法律规定自我推荐,通过选民联名或者组织推荐的方式进行选举；允许候选人在指定区域、时间,按照规定方式进行自我宣传。二是进一步强化人大代表的功能,适当扩大人大的实权,特别是选举权和重大事项决定权要落到实处。<br />
<br />
　　深圳完全有条件创新体制机制,在政治体制改革上先行先试<br />
<br />
　　今后的特区要负起综合改革、建设有中国特色社会主义样板地区的历史使命,就必须负起在政治体制改革方面先行先试的重任。因为特区有“试错权”,有地方立法权,有市场机制比较完善、经济发展基础扎实、新兴城市包袱较轻等一般地区无法比拟的优势；深圳特区还有计划单列、可直通中央、又毗邻香港等优势,完全有条件创新体制机制,在政治体制改革上先行先试,创出更大辉煌,为推动中国的改革开放大业再立新功!<br />
<br />
　　作者系广东省政府发展研究中心巡视员、研究员<br />
<br />
深化行政体制改革:特区未来改革发展开局的突破口<br />
<br />
<br />
　　行政体制改革是政治体制改革的重要内容。特区应当在政治体制上大胆改革,率先突破,为全国探索经验。深化行政、、社会管理改革,为经济体制改革的深化提供综合配套,是当前改革的主要着力点之一<br />
<br />
　　□黄挺<br />
<br />
　　<br />
<br />
　　在特区建立30年的历史节点上,当前改革与发展的着力点在哪里?我认为,当前特区改革与发展的着力点,是要认真总结反思特区建立30年的成功经验及教训,研究当前改革发展面临的突出问题,进一步做好经济特区发展模式转变的研究和战略谋划,科学制定未来30年特区改革发展的总体规划并付诸实施。其中首要的是,着力推进“二次改革”,在继续完善市场经济体制的同时,加大行政体制、社会体制改革力度。以深化行政体制改革作为特区未来30年改革发展开局的突破口和起步点。<br />
<br />
　　深化行政、社会管理改革,为经济体制改革的深化提供综合配套,是当前改革的主要着力点之一。<br />
<br />
　　行政体制改革是政治体制改革的重要内容。特区应当在政治体制上大胆改革,率先突破,为全国探索经验。<br />
<br />
　　一是以政府职能转变为核心进一步完善大部制改革,真正把政府的管理职能从微观管理转向以间接管理手段为主的宏观经济决策,转变到经济调节、市场监管、社会管理、公共服务上来,更多地通过产业政策、行业政策引导企业、服务企业,推动本地区产业结构的调整和升级。要逐步研究城市管理体制改革,实施区以下的大部制改革,尽快研究解决区一级是否保留一级政府、独立财政及四套班子的问题。提升社区的服务功能,财政设立专款,大力扶持社会组织,有计划、分步骤地安排社会组织承接政府职能的转移,并定出承接的时间表。同时,加强管理,为社会组织承接职能后的运转提供服务、指导和监督,建设服务型政府。<br />
<br />
　　二是加快政府管理经济方式的改变,转变依靠行政审批进行管理的模式,进一步大幅度裁减行政审批事项。在落实中央宏观调控政策的条件下,探索建立特区地方性的宏观调控体系,提高特区地方性宏观调控政策的可持续性。完善财税、金融、土地、投融资和收入分配等制度改革,强化市场在资源配置中的基础性作用。<br />
<br />
　　三是以提高行政机关效率为核心进一步深化公务员改革,提高行政机关的效率,适当进行行政业务外包,让市场主体在法律的引导下提供公共产品。<br />
<br />
　　四是把推进党内民主制度的改革作为推动社会民主制度改革的先导,大胆探索。首先,探索加快建立健全特区社会主义基层民主制度,以多种形式、多种渠道推动基层有序的政治参与,通过有序的基层民主参与,培育特区共同发展、和谐发展的城市发展理念。推进建立社会民主监督体系和机制,进一步创新特区财政监督、社会监督的基层民主监督模式。以创新的精神率先试行推进党内民主制度的改革,加快建立制度化的党内民主参与机制,探索完善的党代会代表任期制、试行党代表大会常任制,进一步发挥党的代表大会的决策和监督作用。<br />
<br />
　　五是推行“差额公推直选”制。直选制要从选村官逐步到镇(街)以至区(县)进行试点,稳步推进。从改革人大代表提名方式和党员代表数量入手,进一步优化人大代表的产生方式和职业结构。基层代表产生可以尝试调整为多渠道民主提名；适当减少领导干部代表比例,增加基层干部代表比例；基层党委分步实行“差额公推直选”。进一步优化代表职业结构,适当增加非本市户籍人口的人大代表比例；缩减人大代表与政协委员的交叉任职；选区逐步实行社区、行业协会、功能团体、事业单位混合划分；在候选人提名上,允许行业协会、各功能团体依照法律规定进行自我推荐,通过选民联名或者组织推荐的方式进行选举；允许候选人在指定区域、指定时间,按照规定的方式进行自我宣传。<br />
<br />
　　六是进一步探索加强党委统一领导,充分发挥人大、政府和政协应有的作用。要按宪法规定的人大的权利和责任,进一步强化人大的功能,适当扩大人大的实权,特别是选举权和重大事项决定权要落到实处。要把党管干部的原则与依法办事结合起来,凡属人大任命的政府组成人员,人大应有话事权。要强化人大的监督功能,特别是要加强对公共财政的监管,率先建立起一整套对政府官员的监督体系,形成一套完整的体系和健全的运行机制,特区应当带头建立官员家庭财产报告制度。<br />
<br />
　　作者系广东省体制改革研究会会长<br />
<br />
要在实现公平正义上走在前列<br />
<br />
<br />
　　□高尚全<br />
<br />
　　<br />
<br />
　　以深圳为代表的经济特区已经走过了30年的历程,取得了辉煌的成就,充分发挥了改革开放的试验田、窗口、排头兵和示范区的作用,为我国现代化建设立下了不可磨灭的功勋。特区30年来可以用三句话来概括:思想上大解放,经济上大发展,面貌上大变化。在特区成立30周年的历史背景下再出发,总结特区30年来的经验教训,进一步明确未来特区的发展方向,对再创特区新辉煌具有重要意义。广东经济特区要继续当好排头兵,除了要在探索中国特色社会主义道路上继续走在前列,在转变经济发展方式、发展低碳经济方面走在前列,在鼓励创业创新、发展非公有制经济上继续走在前列外,更应当在实现公平正义上走在前列,为我国的改革开放和现代化建设事业发挥示范带头作用。<br />
<br />
　　温总理在今年的“两会”记者招待会上说,“社会公平正义是社会稳定的基础。公平正义比太阳还要有光辉。我们要继续推进经济体制改革、政治体制改革以及其他方面的改革,根本目的是要促进生产力的发展,要实现社会的公平正义。”总理的讲话引起国内外的强烈反响。我们在推进科学发展、建设和谐社会中,多次提出社会的公平正义。十六届五中全会提出,“要按照构建民主政治、公平正义、诚信友爱、充满活力、安全有序、人与自然和谐相处的社会主义和谐社会的要求,正确处理新形势下人民内部矛盾,认真解决人民群众最关心、最直接、最现实的利益问题。”锦涛同志强调指出,公平正义就是社会各方面利益关系得到妥善地协调,人民内部矛盾和其他社会矛盾得到正确地处理,社会公平正义得到切实地维护和实现。<br />
<br />
　　我们考察一下历史,古希腊的思想家亚里士多得,把公平正义分成分配的公平正义和校正的公平正义。分配的公平正义涉及到财产、荣誉、权力等有价值的东西,对相同的人给予相同的对待,就是公平正义。校正的公平正义涉及到被侵害者的财产、荣誉和权力的多少,不管谁是伤害者,谁是受害者,使受害者从伤害者那里得到补偿,这就是公平正义。现在我们为什么要强调这个问题呢?因为我们出现了收入差距过大,分配不公,影响社会的稳定。目前我国的基尼系数已经超过了警戒线,2009年城乡之间居民收入差距是3.33倍,最富裕的地区和最穷的地区之间的差距是2.68倍,行业之间最高和最低的收入相差11倍；在不同群体之间,最高收入和最低收入群体相差23倍,而且这种差距扩大的趋势还没有得到有效的抑制。收入差距扩大不仅影响到社会的稳定,而且不可避免地造成需求的萎缩。<br />
<br />
　　怎么样实现公平正义,关键还是要靠改革。一是要实现基本公共服务均等化,作为实现公平正义的重要举措。中国要实现公平正义的发展,要实现基本公共服务均等化,这是维护社会公平正义、建设和谐社会的重大的决策。逐步缩小发展过程中出现的城乡差距、地区差距和贫富差距,使全社会成员共享改革发展的成果。二是要搞好初次分配和再分配,这是实现公平正义的重要环节。十七大提出,初次分配和再分配都要处理好效率和公平的关系,再分配要更加注重公平。要调整初次分配的结构,规范初次分配的秩序,提高居民收入在国民收入分配中的比重,提高劳动报酬在初次分配中的比重。中央已经重视这个问题,正在采取措施,逐步实现。再分配要更加注重公平正义,再分配要重点关注困难群体,着力解决困难群体的困难,为困难群体提供基本的社会保障。再分配要处理好公平效率之间的关系,正确处理政府与市场的关系,调动两个积极性,一个是市场的积极性,以效率为基础；一个是政府的积极性,以公平为基础。市场要讲究效率,也要强调公平竞争；政府要强调公平,也要提高效率。一个有效地更好地创造财富,一个更好地实现公平,使两者互相促进,使公平和效率有机地结合起来。三是要加快建立公平正义的体制基础,包括经济体制、社会体制和政治体制等这些方面的基础。<br />
<br />
