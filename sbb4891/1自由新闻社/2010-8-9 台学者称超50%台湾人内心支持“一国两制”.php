													<h2>台学者称超50%台湾人内心支持“一国两制”</h2>
						
						
																											<div id="postmessage_776576" class="t_msgfont">台学者:在台湾内心接受“一国两制”的人超50%<br />
<br />
环球网8月7日消息，台湾“一国两制研究协会”理事长蔡武璋接受媒体专访时表示，“台湾‘独立’在国际现实环境中，根本不可能”，台湾必须与大陆结合才有市场和大好前途，尤其是在大陆崛起以后，两岸应该走向结合在一起的道路。 <br />
<br />
<br />
据悉，蔡武璋是土生土长的嘉义布袋人，具有农业博士学位，是嘉义地方上的知名人士，他在去年11月成立台湾“一国两制研究协会”。 <br />
<br />
对于跨足两岸政治领域的研究工作，蔡武璋在访问中说，他的家乡在嘉义布袋港，1949年以前，两岸还有贸易往来时，布袋港的繁华盛况就像“小上海”一样，但两岸断绝往来后，布袋港也就没落了，他童年时代布袋港繁华的景象，一直留在脑海中，念念不忘，他长大后，也必须和其它年轻人一样离乡背井到各地找工作，当台湾经济成长以后，家乡仍然是一个没落的小地方。 <br />
<br />
蔡武璋表示，在两岸恢复交流以后，家乡父老期盼有一天布袋港能恢复当年的荣景，因此成立“台湾布袋港发展促进会”，地方父老并推举他担任理事长，从此以后，他就开始从事推动两岸经贸、文化、农业领域的交流工作，至今也将近15年了。 <br />
<br />
蔡武璋说，他早期从台湾中南部一路走来，看到许多农民在农产品盛产时，尤其是水果，收入常常不敷成本，在大陆开放台湾水果零关税进入大陆市场以后，对台湾农民是一大福音，也是一大机会。 <br />
<br />
他说，国民党和民进党为了台湾前途，有截然不同的主张，而台湾“独立”在国际现实环境中，根本不可能，因为台湾是一个海岛，必须与大陆结合才有市场和大好前途，尤其是在大陆崛起以后，两岸应该走向结合在一起的道路。 <br />
<br />
蔡武璋认为，两岸未来最好的结合方法，就是一国两制，而他号召成立台湾“一国两制研究协会”，协会成员大都是台湾本土的企业家和学者，主要目的，就是推动朝向这个对台湾未来最好的方向，虽然台湾民意目前大多数仍抱持“保持现状”的看法，但对于一国两制，他认为已经慢慢有民众开始接受，希望透过各种研究，提供给两岸有关单位做为参考。 <br />
<br />
蔡武璋还说，在当前两岸关系发展之下，“一国两制研究协会”认为两岸第一优先的，就是签署和平协议的议题，这也是大部分台湾民众想要的、一个更有安全感的协议。他认为，两岸商签和平协议的过程当中，大陆会考虑台湾的需要，如果马英九能在2012年连任，在新的四年任期当中，应当是最适当的签署时机。 <br />
<br />
对于“两岸在和平统一的理念之下，何时能完成这个历史的使命？” 蔡武璋认为，在马英九两个任期之内，都不至于会有结果，而在大陆经济快速发展的状况下，也许不超过20年，台湾同胞会看到大陆经济发展成为强盛的世界舞台，到时候可能会有大部分台湾同胞，转向支持和平统一的两岸关系。 <br />
<br />
在蔡武璋看来，“两岸若要和平统一，台湾的要件就是一国两制”，而且条件要与香港、澳门不同，例如香港保持50年制度不变，台湾可以要求100年制度不变。甚至台湾保有自己的军队，大陆不派军队到台湾；台湾每年的税收不必上缴北京等等。 <br />
<br />
蔡武璋坦言，在他成立“一国两制研究协会”后，一些民进党的朋友就不和他来往了，对此他能够体谅，不过他也感叹，很多民进党的人士也都赴大陆交流和投资，而且也都赚了不少钱，“民进党不能心里想着台独，在现实上又想在大陆赚钱，这种情形我看多了”。 <br />
<br />
蔡武璋也受聘于福建省，担任“闽台经济合作促进委员会”委员，但大陆“海西经济特区”迄今未获马政府的重视。蔡武璋表示，台湾当局可能认为海西经济区将吸引很多台商前往投资，所以马政府对于海西经济区没有正面的鼓励。但他认为，在商言商，台商会选择往哪里走，海西经济区既然在土地、劳工成本具有吸引力，台商就会考虑成本，一定会有人去参与海西的建设，将来两岸若更进一步结合，海西经济区将是台商最优先选择的地点。 <br />
<br />
蔡武璋曾表示，在台湾，内心支持“一国两制”的人超过50%，有这样的群众基础，再加上国民党上台后两岸交流的加速，现在正是要有人站出来推动这项工作的时候。未来，他将让研究协会在台湾北、中、南全面开花，说服台湾同胞了解、接受“一国两制”，推动两岸最终统一。</div>

							
							
							
							
