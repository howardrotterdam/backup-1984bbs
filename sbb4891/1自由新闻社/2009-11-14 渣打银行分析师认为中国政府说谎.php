													<h2>渣打银行分析师认为中国政府说谎</h2>
						
						
																											<div id="postmessage_326233" class="t_msgfont">水泥不会说谎 渣打银行称中国投资增速被高估<br />
2009年11月13日 21:42经济观察报<br />
<br />
蓝、黑、浅蓝。三个颜色的曲线在同一个经济图表中纠结着上上下下，蓝色代表投资增速，黑色代表钢材产量增速，浅蓝代表水泥产量增速。在图表上，它们从2008年四季度开始出现明显的分歧。<br />
<br />
投行分析师对中国官方数据的不信任感就从这里开始。王志浩说，我们怀疑中国的统计数据有些是假的。但我们相信水泥，因为水泥不会说谎。<br />
<br />
王志浩是渣打银行中国区研究主管。水泥是王志浩最信得过的指标之一。他说，“水泥产量是衡量中国投资增速的最可靠指标，因为水泥有保质期，不能长时间储存，而且所有的建设投资几乎都会用到水泥”。<br />
<br />
中国国家统计局每月中旬都会发布相关统计数据，包括投资、消费和物价指数等。一般情况下，王志浩总会在统计局的数据发布后不久重新核算。不过他发觉，等待他的往往是很不一样的结果。<br />
<br />
在他展示的一张图表中，有一段时间，水泥产量、钢材产量和投资增速分歧明显，在官方发布的数据中，当投资增长曲线持续上扬的时候，水泥产量保持不变，钢材产量急速下滑。这种情形发生在2008年四季度到2009年的一季度，是中国经济遭遇最为严重下滑的时刻。<br />
<br />
水泥和钢材是投资建设中必不可少的原材料，与钢材存在库存的情形不同，水泥不能长时间储存。一般来说，水泥的保质期只有一个月，超过这个时间，水泥的效能就要大大降低。在王志浩的图表上，从2008年四季度到2009年一季度的大部分时间内，中国水泥产量的增速为零。<br />
<br />
国家统计局的数据显示，2008年四季度中国GDP增长6.8%，2009年一季度GDP增长6.1%。王志浩认为，实际情况没有那么好。“去年四季度和今年一季度是最糟糕的时候，地方政府可能出于中央担心经济下滑的原因，虚报了数据。”<br />
<br />
这个一直在关注中国经济变化的外国人“开发”出了自己的衡量体系。跟王志浩一样对中国官方统计数据持审慎态度的观察人士，也喜欢用一些替代性指标来观察中国经济。比如说，发电量一度是他们判断中国经济冷热的最喜欢用的指标之一。不过，这也让他们对中国统计数据有了更大的困惑。<br />
<br />
今年一季度，中国工业生产在第一季度增加了5.1%，但是发电量却下降了3%，就引起了不小的争议。国家统计局发言人李晓超承认，直接从数据上很难解释，不过他说：“工业增长速度是准确的，用电量也是准确的。”<br />
<br />
对于发电量和经济走势不匹配的质疑，国际货币基金组织在五年之前就曾提出过。一般来说，经济处于波动期时，中国统计数据面对的质疑会更多。1998年之后，中国统计数据就曾遭遇外界的疑问，这种疑问一直延续到2003年之后才渐渐淡去。不过，自去年四季度后，质疑之声又开始增多。<br />
<br />
国家统计局官员承认一些地方可能出于政绩考虑而虚报数据，不过他们坚持统计数据总体上是真实可信的。为了保证数据的可靠性，2008年全年，全国立案查处统计违法案件有17000多起。今年5月份，有监察部参与的一份文件中，也明确将对造成统计数据不实的各类违法违纪行为进行处罚。<br />
<br />
不过地方与中央数据对不上的情形并未绝迹。今年上半年，国家统计局数据显示，全国GDP同比增长7.1%。各地公布的数据则显示，有24个省份GDP增速高于全国速度。地方GDP之和超全国1.4万亿元。<br />
<br />
实际上，地方公布的GDP平均增幅常年高于国家统计局数据两个百分点左右，但是去年四季度和今年一季度却出现了反常迹象。王志浩发现，去年四季度和今年一季度，地方政府的投资数据和GDP数据之和实际上低于国家统计局最终的发布结果。这或许并不奇怪――这段时间内，地方政府正在为分羹1.18万亿中央投资而竞争。<br />
<br />
这让一些投资者心有疑虑。国家信息中心的一名专家说，一些机构和企业在聘请我们做分析调研时，甚至会直接提醒我们注意指标数据的可靠性。比如，衡量工业生产的恢复状况时，提出对用电量的定量分析；分析外贸出口状况时，他们看的不是进出口数据，而是港口数据；分析海运时，他们更为关心的是运输的船型。<br />
<br />
王志浩说，如果只看官方数据，在扣除物价指数后，你会觉得中国的投资已经到了顶点，但是如果看水泥，你会发现，其实中国的投资可能正处在一个新的上涨周期中。王志浩的结论是，通过水泥表现出的投资增速，要比统计局发布的投资增速低5到10个百分点。<br />
<br />
他说：“没有水泥和钢材，我不知道投资增长依据的是什么。”</div>

							
							
							
							
