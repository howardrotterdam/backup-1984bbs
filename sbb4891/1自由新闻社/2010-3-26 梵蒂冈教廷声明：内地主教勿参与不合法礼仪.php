教廷声明：内地主教勿参与不合法礼仪<br />
<br />
梵蒂冈中国问题委员会结束会议后，教廷发声明提醒内地主教不要参与不合法的圣祭礼仪、会议等。陈日君枢机（图）是该委员会成员之一。<br />
<br />
<br />
【本报讯】对于内地非法教会团体即将选出新一届领导、以威迫利诱方式迫使内地主教参与选举，教廷已不耐烦，在中国问题委员会三日会议后，罕有地发表声明，提醒内地主教不要参与不合法的圣祭礼仪、主教祝圣及会议，明确指出这些活动与教宗的意愿相悖。<br />
<br />
即抵制爱国会与主教团<br />
<br />
教廷本周一至周三举行一连三日的中国问题委员会会议。委员会 07年成立，由教廷多个部门的主管、中国教会及修会代表组成，成员包括天主教香港教区荣休主教陈日君枢机、现任主教汤汉，以及台湾、澳门等地主教。<br />
<br />
教廷的声明提醒内地主教不要参与不合法的圣祭礼仪、主教祝圣及会议。委员会了解内地教会传教工作困难，神职人员的自由被剥夺。教廷相信，透过与内地政府坦诚对话，问题可以解决，中梵双方可以达致共识。<br />
<br />
圣神研究中心高级研究员林瑞琪表示，教廷在闭门会议后公开发声明，提醒主教不要参与不合法的礼仪，相当罕见。他解释，内地下半年将举行天主教代表大会，受国家控制、不获教廷认可的主教团及爱国会将选出新一届领导，代表大会权力凌驾主教团，主教团可直接干预地区教会，「呢个安排本身就同体制不符，地方教区无可能畀无关民间组织控制住。」<br />
<br />
他强调，根据《天主教法典》，主教只由教宗任命，不容许任何人干预，「&#60811;家系罩鹘躺厦婕恿礁鎏上皇，一个系主教团，一个系天主教代表大会，主教唔可以再畀爱国会控制，今次呢个声明罡叩鳎系想刻意提醒主教，发出一个清楚讯息，唔好参与任何有违体制行动。」<br />
<br />
料内地短期再私自祝圣<br />
<br />
为令天主教代表大会更具认受性，内地非法的教会领导以不同方式威胁神职人员参加会议。林瑞琪预计，短期内将有不合法的主教祝圣，「因为过去几年好多教区主教都过身， 08、 09年又又鹘套Ｊス，有需要补番。」</div>

							
							
							
							
