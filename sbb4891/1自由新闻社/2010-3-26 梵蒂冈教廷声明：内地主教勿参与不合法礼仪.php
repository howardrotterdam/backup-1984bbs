<br />
沙甸事件”概述<br />
――搞自《沙甸回族史料》&nbsp;&nbsp;原作者：马绍美<br />
<br />
十年文革”酿成了“沙甸事件”。“沙甸事件”给沙甸人带来的损失是惨重的。这个损失比较起整个国家“十年浩劫”中的损失来虽不算大，但它记下了我们党在“十年文革”中对少数民族问题、宗教问题采取的左的态度以至被林彪、江青两个反革命集团利用，造成了回家重大的损失，有着极鲜明的代表性。“十年文革中的沙甸”可谓我国“十年文革”的缩影和写照。<br />
<br />
一九六七年轰轰烈烈的“革命风暴”席卷着满天的传单、大字报以及人们莫衷一是的心情来到沙甸。这是毛泽东同志发动的。耿直的沙甸回民当然热烈响应。丢了锄头、镰刀、扁担、谷箩，举起纸裱的小红旗，成立了毛泽东思想的“造反队”、“战斗队”、“兵团”。听上边说清真寺在被扫的“四旧”之列，于是，几个人带领鼓动，部分群众拿出了“舍得一身剐，敢把皇帝拉下马”的精神，疯狂地烧起宗教经典和“地富反坏右”家的书籍来。那气势，若秦始皇在世也是要自叹不如的。<br />
<br />
“毛泽东思想大无畏造反兵团”、“毛泽东思想红色造反队”冲击关闭了清真寺。<br />
<br />
沙甸最能显示力量的“民族政策捍卫兵团”站出来，“红旗野战兵团”也站了出来。<br />
<br />
　<br />
<br />
一场争夺清真寺的斗争展开了<br />
<br />
　<br />
<br />
拉锯争夺清真寺越演越烈，沙甸马上成了“造反”与“捍卫”，“革命”与“复辟”的勇士们的用武之地――开始武斗，刀枪相见。<br />
<br />
大家都把自己的知识、谋略尽情地具体发挥，对自己的“敌人”要进行“革命的大扫除”了。<br />
<br />
沙甸的“左派”人数不多，但靠山硬，去部队那里领了武器，带着其它地方的“左派”，来攻打“复辟”的沙甸。一九六八年七月，包围了沙甸，炮打烂了清真寺的屋顶和部分民房。双方都死了人，沙甸没被攻破。<br />
<br />
一部分所谓站错队的其它民族的同志到沙甸避难。对沙甸就要“捅马蜂窝”。<br />
<br />
六八年十一月，称为云南省革命委员会一、二号文件的宣传队（支左部队）包围了沙甸，并展开了舆论攻势。<br />
<br />
“一小撮阶级敌人、牛鬼蛇神、野心家、阴谋家受国民党军统特务指使，煽阴风，点鬼火，欺骗群众，蒙蔽群众，妄图颠覆无产阶级专政，他们必将碰得头破血流！”<br />
<br />
“谁反对毛泽东思想就没有好下场！”<br />
<br />
“谁胆敢放一枪一弹就坚决镇压！”<br />
<br />
“受蒙蔽无罪！反戈一击有功！”<br />
<br />
……<br />
<br />
在强有力的宣传攻势下，大家都把希望寄托在“宣传队”会很好执行党的政策上，于是把准备对抗的武器全部交了出去。六八年十二月八日，沙甸回民夹道欢迎宣传队（解放军）进村。<br />
<br />
“宣传队”进住清真寺。吃猪肉，猪骨头丢在水井里。<br />
<br />
清理阶级队伍，“反军乱军黑干将”、“宗教复辟急先锋”、“跳梁小丑”、“小爬虫”抓起了两百多。对这些人专政，反复开斗争、批判大会，捆、梆、吊、打，有人在吊打中死去。强迫这些人学猪叫、学猪爬、学猪滚，有一孕妇滚得小产。<br />
<br />
极左路线的推行者，要用“行动”来证明他们是“最最革命的”。狠狠打击信教者，正是左的革命表现。沙甸搞以教划线，礼拜、把斋者是批斗对象。公开宣布：“信教就是反对马列主义、毛泽东思想，就是反对共产党的领导”。<br />
<br />
“抓革命、促生产”，沙甸的几百“牛鬼蛇神”抓出来了，生产“上去了”，学大寨的标兵生产队每个劳动日值8角，其它生产队还在2角、3角、4角中徘徊。有一个共同点就是九个生产队都评政治工分，政治思想好的人工分就多，否则就少。<br />
<br />
在艰苦漫长的岁月里生存发展起来的回回民族，有强烈的民族自尊心和宗教感情，团结力最坚，斗争性最强，有优良的传统、有光辉的文化遗产，能够接受真理、激起正义，不能接受对自己的侮辱与糟蹋。<br />
<br />
为了维护党纪国法的尊严，为了维护回回民族的尊严，为了得到正当的生存权利，保持自己的风俗习惯、信仰自由，一九七三年十月，在马伯华等同志带领下，以开放清真寺为起点，沙甸等地回民拉开了与“四人帮”针锋相对斗争的序幕。<br />
<br />
开放清真寺被说成是“宗教复辟”，州县工作队进村，调子更进一步：“民族斗争说到底就是阶级斗争……”。<br />
<br />
回民的宗教就是伊斯兰教，而复辟就是被镇压了的东西东山再起。大家知道共产党有一条“宗教信仰自由”的政策，却没有听说过伊斯兰教被镇压了。<br />
<br />
“批林批孔”，保卫无产阶级文化大革命的伟大成果的工作队在坚定不移地进行斗争，群众始终没被压下去。<br />
<br />
过开斋节被说成“反革命集会”，进行武装阻拦未奏效。<br />
<br />
拉锯争夺清真寺越演越烈，群众始终占上风……<br />
<br />
一九七四年五月底，蒙自县公安局抓马绍美，因此群众包围了工作队驻地，强烈要求放人。工作队却卷起行李溜了。<br />
<br />
一九七四年八月，周兴到沙甸，承认党的宗教信仰自由政策还存在，答复可以清查生产队的经济帐目。周兴离开沙甸，前言尽否，“黑帐”依旧……<br />
<br />
一九七四年十月，沙甸等地回民上访（昆明）要求落实政策，要求解决在少数民族地方存在的经济、民族、宗教方面的问题。<br />
<br />
“这是闹事”。“这是反对党的领导”。“四人帮”、周兴等人是会做帽子的。<br />
<br />
鸡街地区成立了“民兵联合指挥部”。民兵进行示威，用大字报“揭露”沙甸“一小撮反革命分子披着宗教外衣反党反社会主义、反毛泽东思想”。<br />
<br />
帽子太大了是戴不稳的 <br />
<br />
一九七四年十二月初，沙甸成立“回民民兵团”。 <br />
<br />
一九七四年十二月中旬，当时的云南省委常委一要员到沙甸，鸡街民兵联合指挥部的民兵借口保驾包围沙甸，荷枪实弹，进行威胁。沙甸回民不甘受辱，徒手夺枪两支，但也付出了死亡一人、重伤三人的巨大代价。对方民兵虽被回民俘虏七人，都得到回民的优待，教育后而放归。 <br />
<br />
一九七四年十二月十七日至二十三日，沙甸落实政策宣传队发动了建水县回龙、馆驿、麻栗树等地回民开放了他们的清真寺。 <br />
<br />
一九七四年十二月二十七日至二十九日，沙甸回民民兵前往建水解救回龙被对方民兵包围一事。 <br />
<br />
一九七四年十二月三十一日沙甸回民在要求蒙自县委落实政策、解决问题未砀情况下冲击了县委机关和县武装部，被对方民兵骗到蒙自二中，打死我十四人，重伤十六人，回民一怒之下，攻下对方，击毙其十二人。<br />
<br />
一九七五年元月一日，中央通知马伯华、郑全书、马绍美等回民代表十人与云南省委一起赴京解决问题。做了两天的思想工作后，元月三日晚，回民代表与省委常委一起，乘坐国务院派来的专机抵达北京。<br />
<br />
一九七五年元月上旬，贯彻中共中央一九七五二号文件，即关于浙江、云南停止武斗，收缴武器的通知。云南省委为贯彻此文件，专门成立了云南收缴枪支执行小组，由十四军当时的政治部主任冯治国任组长，蒙自军分区当时的司令员李凤刚和沙甸的马绍美任组长，开远、建水当时的县委书记邓子俊、黄昌岐以及回民代表马先庆、马恩俊任组员。这个组的分工是：马绍美、马先庆、马恩俊收缴回民方面的武器，冯治国行商和个收缴其他民族的武器。半个月时间，沙甸等地回民上缴了全部枪支弹药。据冯治国等几个说其他民族的也缴清了。执行小组回北京汇报。<br />
<br />
一九七五年二月中旬，建水县馆驿等村回民集中到回族村子――双山做圣节。<br />
<br />
香木桥大队肖家庄的民兵持枪前往威胁挑衅，做节的回民在惊怒之下欲夺其枪，未成，反被打死九人。引起了在二月下旬沙甸等地回民的第二次赴昆上访。<br />
<br />
三月五日王洪文令驻玉溪的部队包围交开炮轰击玉溪西营清真寺。玉溪昆明等地开始抓捕“闹事”的回民头头。沙甸空气紧张，人心惶惶。<br />
<br />
住北京的周兴等人得意忘形，向住北京的回民代表声称：“这次到北京是一次重新‘站队’难得的‘机会’，年轻人，前途无量，要好好考虑啊！”回民代表没买帐。<br />
<br />
五月初，几个团的部队要进驻沙甸，群众怕重吃一九六八年部队进村后的亏，竭力阻拦进村，因此部队未进成。<br />
<br />
五月二十三日，周兴等人以云南省委的名义发了“通告”，并用飞机到沙甸上空撒发。“通告”把回民群众要求落实政策说成是被“反动教长”和“反革命分子”煽动起来的“闹事”，几个回民头头是违法的，只有放下武器（其实没有武器），迎接解放军进村，才是唯一出路。 <br />
<br />
回民群众义愤填膺，几千份“通告”一下就被集中成几大麻袋，小伙子们簇拥着扛到工作队驻地――鸡街糖厂的大门口，一把火烧了。<br />
<br />
谣言漫天，污蔑、诬告回民的黑材料不断送上北京。<br />
<br />
当时红河州的一个主要帮派人物说：“现在回民就象一头疯牛，我们要把她（他）引入火炕。”<br />
<br />
七月中旬，“四人帮”及云南的周兴等人命令回民代表回沙甸做工作，交出武器，（特别强调交出新式武器）和欢迎部队进村。<br />
<br />
没有什么新式武器，当然也就无法交出。关于自制的土武器，大家表示在接受群众的条件后就交。条件即：欢迎少而精的部队进村落实政策。沙甸这么一个村子，怎能容纳几个团的部队？解放沙甸时，对付国民党的一个团只需要一连的解放军，今天到沙甸落实政策却要进驻几个团的部队，要搞什么呢？六八年部队进村的教训，大家记忆犹新……<br />
<br />
群众坚持只欢迎少数部队进村落实政策的意见。<br />
<br />
“四人帮”及其一伙欺骗的伎俩用完了，最后，无耻地给沙甸等地回民扣上“与苏修、敌特挂钩，要成立伊斯兰共和国、要叛乱、有地道、有新式武器”的政治罪名。险恶用心，昭然若揭。可惜回回太幼稚、耿直，知道得太晚了。<br />
<br />
一九七五年七月二十九日凌晨三点，“四人帮”开始对沙甸回民武装镇压。村边站岗的回民民兵，被把手脚捆起来丢到粪池中溺殁；沙甸父老被枪炮声惊醒；大清真寺的守卫者殉难；沙甸大清真寺被武装人员占领。<br />
<br />
凌晨四点多，沙甸回民凭据地形熟，进行反攻。付出上百人殉教的巨大代价，夺回了。大清真寺，始得到一点现代武器的装备。此时，西营（注：老沙甸西部村名称谓）已被武装人员占领，后山、马家井、林家巷、金鸡寨、川方寨的部分生产队场院及民房也武装人员控制，于是，在这黎明前的黑暗里，街战、巷战、以住房为据点的攻守战在残酷地进行着，在火箭筒的爆炸声、火光里，在枪炮的呼啸声中回民一片片倒下。沙甸人没有屈服。到天亮时，又收复了部分民房据点，收回几间自己的房屋，又付出几百条宝贵生命的代价。更为凄惨者，没抵抗的回回们――<br />
<br />
有的被打死在堂屋（回话会客室）中，有的被击毙于门旁，甚至有的被枪杀于床上；有的指望逃生，愿意当俘虏被押出村去，却在押送途中被枪击于路旁的光天化日之下……<br />
<br />
群情震怒，凭据用无数生命换来的那几支步枪、机枪、手榴弹，以及那些自制的土武器奋起抵抗。有多少本不想抵抗的人，最后也含酸忍泪拿起那点土炸药去寻同归于尽的对象。然而真正找到同归于尽的对象的并不多，自己反而丧生的却不少。<br />
<br />
炮火越来越猛烈。中午，三所清真寺开始被炮击，哪里的房屋坚固哪里被炮击。哪里出现炊烟哪里被炮击，哪里有水井哪里就被火力封锁。无须说明，大家都清楚了，这是沙甸穆斯林的灭顶之灾。于是，以安拉的考验、以乐园的恩典、以回回民族的气节相互安慰着、鼓励着、遗嘱着……此时，望生哪有生之路，望存哪有存之地？<br />
<br />
二十九日入夜，炮火有所减弱。但只要哪里房屋透出光亮，随着炮弹就划破黑暗，呼啸着在哪里爆炸，于是惨痛的呻吟声、幼儿被惊吓的哭声……撕肝裂肺。黑夜，为何这般漫长！你要吞噬多少人的生命和鲜血啊！<br />
<br />
三十日，进攻的武装人员放弃对老沙甸村内的部分控制点，缩小村外包围圈，占领制高点及有利地形，在无线电报话员的指挥下，对老沙甸村子进行更猛烈的轰击，火焰喷射器也用上，老沙甸成了一片火海。金鸡寨、川方寨炮火稍逊。逊下午，老沙甸部分群众陆续撤到金鸡寨。入夜，马伯华带领部分群众也撤到金鸡寨。通过商议，决定做群众的思想工作，出村逃生，身体健康者可设法转移。<br />
<br />
三十一日，通过反复劝说，二千余老弱妇幼、伤残病的回民群众在笔者的带领下集队出村，以求生存。出村的群众马上被分住几个小集中营办学习班，笔者等头头锒铛入狱。入夜，突围转移未成，在川方寨发生遭遇战，伤亡惨重，马伯华脚被手榴弹炸伤，只好撤回金鸡寨。<br />
<br />
八月一、二这两昼两夜中，金鸡寨、川方寨两村的房屋据点争夺战从未间断。一方是拥有现代化武器装备，一方仅凭精神；一方是训练有素的武装人员，一方是普普通通的平民百姓，怎样拚呀！在炮火硝烟迷漫中，在血洗之后，老沙甸与川方寨已没有抵抗力量了。<br />
<br />
八月三日，金鸡寨已被进攻人员控制一半左右，争夺、拼搏仍在进行。炮火依然未断。在已被进攻人员控制的地段，残酷的“清剿”在继续，伤残的“回匪俘虏”被提着脚手，象丢死羊一样丢上汽车，运去“医治”，而有的干脆在现场补枪，来个“快性”。<br />
<br />
八月四日，一百五十七名男女老幼回族群众举着双手，集队从金鸡寨南面出村，指望求生。当走到大田埂上的时候，几挺机枪一齐开火，一分钟之后，尸横遍地，血流成渠，随即验尸，未亡者进行补枪。现有五名幸存者以为铁证，其中三名是补枪也未补死的。下午六时许，炮火摧毁了金鸡寨南面几间仅存的小民房，马伯华等同志壮烈殉难。入夜，又进行了最后的“清剿”，战斗始告结束。<br />
<br />
这七天八夜，不知耗费了多少弹药。被摧毁的民房四千四百多间，被夺去生命的沙甸回民九百人，伤残六百余人，进剿者死伤人数不得而知。以参加“沙甸叛乱”的罪名遭到同样武装镇压的还有开远县的新寨、砚山县的车白泥、田心茂龙、松毛坡、文山的茂克等。整个事件中回民共死亡一千六百余人。“沙甸事件”惨绝人寰，在新中国的民族史上写上了最不光彩的一页。<br />
<br />
中国共产党老一辈无产阶级革命家拨乱反正、正本清源，“四人帮”被粉碎了。他们破坏社会主义法制的罪行得到了应有的惩罚。真理胜利了！人民胜利了！沙甸回民又见到了社会主义的阳光。<br />
<br />
一九七九年二月，中共云南省委、昆明军区党委联发了（79）7号文件，即“关于沙甸事件的平反通知”。文件指出：“一九六八年以来，由于潭甫仁、周兴推行林彪、‘四人帮’的反革命修正主义路线，大搞以人划线、层层站队，支一派、压一派，严重分裂了各族干部群众，甚至采取了侮辱回民群众的错误做法，伤害了民族感情，破坏了党的民族政策和宗教政策，破坏了民族团结，引起了沙甸等地回族群众的强烈不满。问题发生后，一九七四年五月，中央作了正确指示，要周兴亲自处理，落实政策，周兴却坚持错误、采取一系列激化矛盾的错误做法，酿成了一九七五年七月沙甸事件，造成了严重后果。因此，沙甸事件并不是反革命叛乱，采取军事解决是错误的。经党中央批准，原定（以沙甸为中心的反革命武装叛乱）的结论应予撤销，这个事件中涉及的广大回族干部群众应予平反。”<br />
<br />
中共云南省委民族工作部同时发了（79）1号文件。即“关于处理沙甸事件遗留问题的通知”。国家补助修建了七所清真寺。落实民族政策和宗教政策。<br />
<br />
一九八○年七月，省委民族工作部又发了（80）142号文件，即“关于沙甸事件平反善后经费的若干补充意见”。按照文件精神，红河州委、个旧市委处理了孤老、孤儿、半孤儿以及死难家属的抚恤救济问题、伤残人员的医疗补助问题。<br />
<br />
全民所有制单位招收了二百名符合招工条件的孤儿及受害家属子女；沙甸划为经济作物区，吃国家返销粮。<br />
<br />
一九七八年八月，针对云南省委、昆明军区党委联发（79）7号文件第3条留下的尾巴，云南省委发了（87）31号文件，即《关于撤销中共云南省委、中共昆明军区委员会联发（1997）7号文件中个别结论的通知》。<br />
<br />
（87）31号文件指出：“……实践证明，这个文件[(79)7号文件,笔者注]总的是正确的.在贯彻执行过程中,也发现文件第三条中关于“马伯华、马开志、郑全书、马绍美等几个人对抗中央指示，使矛盾激化，是有错误的。特别是他们成立非法秘密组织，妄图背叛祖国，搞打砸抢造成流血事件，破坏民族团结，是有确凿罪行的。但考虑到历史原因，马伯华、郑全书、马绍美可以不作为反革命分子对待。”这一结论不确，经过反复查证，其结果是：<br />
<br />
一、关于“非法秘密组织”，是指“罕指奔位协”，它是在“文化大革命”不正常的历史条件下，少数人搞起来的一个宗教性组织，不应作为罪行。<br />
<br />
二、关于“妄图背叛祖国”问题，查无实据。所谓背叛祖国的罪行不能成立，应予否定。<br />
<br />
三、关于打砸抢问题，确实是存在的，打砸抢是错误的。鉴于“沙甸事件”这一特殊情况，不再追究。<br />
<br />
据此，经请示中央同意，将联发（79）7号文件第三条结论，予以撤销。”<br />
<br />
至此，“沙甸事件”完全、彻底平反了。这是党中央对广大回民的关怀。经受冰霜摧残的禾苗，最能感到阳光的温暖，深受林彪、“四人帮”残酷迫害的沙甸穆斯林，最能体会党的恩情，同时，沙甸回民还知道，洗劫之后的祖国及家乡，文化、经济恢复发展的重任摆在我们面前。历史的经验使我们知道，我们需要安定团结。这样，我们才能为祖国的四化建设，为回回民族的文明富裕而奋斗。<br />
<br />
一九八八年五月初稿&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;<br />
<br />
一九八八年十月定稿</div>

							
							
							
							
