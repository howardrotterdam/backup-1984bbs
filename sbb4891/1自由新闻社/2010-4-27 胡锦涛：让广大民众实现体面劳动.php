新华网北京４月２７日电<br />
<br />
在２０１０年全国劳动模范和先进工作者表彰大会上的讲话<br />
<br />
（２０１０年４月２７日）<br />
<br />
胡锦涛<br />
<br />
同志们：<br />
<br />
　　在全世界工人阶级和劳动群众的光辉节日――“五一”国际劳动节即将来临之际，我们在这里集会，隆重表彰全国劳动模范和先进工作者，这对激励和鼓舞全国各族人民满怀豪情地推进全面建设小康社会进程、不断开创中国特色社会主义事业新局面，对激励和鼓舞青海玉树地震灾区各族干部群众、广大救援人员和全国各族人民信心百倍地战胜一切艰难险阻、全面做好抗震救灾工作，具有十分重要的意义。<br />
<br />
　　首先，我代表党中央、国务院，向全国各族工人、农民、知识分子和其他各阶层劳动群众，向人民解放军指战员、武警部队官兵和公安民警，向香港特别行政区同胞、澳门特别行政区同胞、台湾同胞和海外侨胞，致以节日的祝贺！向为改革开放和社会主义现代化建设作出突出贡献的劳动模范和先进工作者，致以崇高的敬意！我还要代表中国工人阶级和广大劳动群众，向全世界工人阶级和广大劳动群众，致以诚挚的问候！<br />
<br />
　　我国工人阶级是我国先进生产力和生产关系的代表，是我们党最坚实最可靠的阶级基础，是社会主义中国当之无愧的领导阶级，是全面建设小康社会、坚持和发展中国特色社会主义的主力军。长期以来，在中国共产党领导下，我国工人阶级和广大劳动群众始终站在时代前列，积极投身革命、建设、改革的洪流，艰苦奋斗，锐意进取，为国家、为民族建立了伟大历史功勋。新中国成立60多年来特别是改革开放30多年来我国经济社会发展的伟大实践和辉煌成就，充分显示了我国工人阶级和广大劳动群众的聪明才智和创造活力。<br />
<br />
　　2005年全国劳动模范和先进工作者表彰大会以来，我们高举中国特色社会主义伟大旗帜，以邓小平理论和“三个代表”重要思想为指导，深入贯彻落实科学发展观，成功举办北京奥运会、残奥会，隆重庆祝党的十一届三中全会召开30周年、新中国成立60周年，圆满完成载人航天飞行和首次月球探测工程，奋力抗击四川汶川特大地震和青海玉树强烈地震等重大自然灾害、迅速开展灾后恢复重建，有力应对国际金融危机冲击、保持经济平稳较快发展，精心筹办上海世博会，着力加强民族团结、维护社会和谐稳定，全面推进社会主义经济建设、政治建设、文化建设、社会建设以及生态文明建设和党的建设，书写了改革开放和社会主义现代化建设恢宏壮丽的时代篇章。这些成就的取得，是全国各族人民在中国共产党领导下同心同德、奋力拼搏的结果，是我国工人阶级和广大劳动群众团结一心、辛勤劳动的结果。今天受到表彰的全国劳动模范和先进工作者就是我国亿万劳动群众的杰出代表。长期以来，你们在各自岗位上展现主人风采、焕发劳动激情，为改革开放和社会主义现代化建设作出了突出贡献，铸就了信念坚定、立场鲜明，艰苦奋斗、勇于奉献，胸怀大局、纪律严明，开拓创新、自强不息的工人阶级伟大品格，在共和国的旗帜上镌刻了人民伟大、劳动神圣的无尚荣光。党和人民感谢你们，全社会都要向你们学习。<br />
<br />
　　当前，国际形势继续发生深刻复杂变化，我国继续处在经济社会发展的重要战略机遇期。在国际竞争日趋激烈、国内改革发展稳定任务艰巨繁重的情况下，认清机遇、抓住机遇、用好机遇，正视挑战、迎接挑战、战胜挑战，继续解放思想，坚持改革开放，推动科学发展，促进社会和谐，是全国各族人民的共同任务。在当代中国，工人阶级和广大劳动群众始终是推动我国经济社会发展、维护社会安定团结的根本力量。实现我们确定的宏伟目标，必须高度重视和充分发挥我国工人阶级和广大劳动群众的主力军作用。我国工人阶级和广大劳动群众一定要肩负起光荣的历史责任，继续团结拼搏、奋发努力，在前进道路上不断建立新的业绩。<br />
<br />
　　第一，进一步弘扬劳模精神，为激励全国各族人民团结奋斗凝聚强大精神力量。成就任何一项伟业都离不开劳动。要实现全面建设小康社会、进而基本实现现代化的宏伟目标，必须依靠全体人民热爱劳动、勤奋劳动，必须依靠全社会尊重劳动、保护劳动，必须使通过诚实劳动创造美好生活成为亿万人民的共同追求。榜样蕴藏无穷力量，精神激发奋斗意志。爱岗敬业、争创一流，艰苦奋斗、勇于创新，淡泊名利、甘于奉献的伟大劳模精神，是中国工人阶级崇高品格的生动体现，是我们时代的宝贵财富，是激励全国各族人民团结奋斗、勇往直前的强大精神力量。我们一定要在全社会大力弘扬劳模精神，用劳模的先进事迹感召人民群众，用劳模的优秀品质引领社会风尚，充分发挥劳模的骨干和带头作用，在全社会进一步形成崇尚劳模、学习劳模、争当劳模、关爱劳模的良好氛围。要真诚帮助劳模解决工作、学习、生活中的实际问题，为劳模发挥聪明才智、建功立业营造更好环境和条件。受到表彰的劳动模范和先进工作者要珍惜荣誉、戒骄戒躁，发扬成绩、再接再厉，在全面建设小康社会的征途上为祖国、为人民、为民族再立新功。<br />
<br />
　　第二，进一步激发创造活力，为推动经济又好又快发展积极贡献力量。发展是解决中国一切问题的“总钥匙”。推动经济又好又快发展对全面建设小康社会、加快推进社会主义现代化，对开创中国特色社会主义事业新局面、实现中华民族伟大复兴，具有决定性意义。我们一定要坚定不移坚持发展是硬道理的战略思想，深入贯彻落实科学发展观，牢牢扭住经济建设这个中心，紧紧把握全面建设小康社会、坚持和发展中国特色社会主义这个当代中国工人运动的主题，坚持聚精会神搞建设、一心一意谋发展，同时一定要加快经济发展方式转变和经济结构调整，不断提高发展质量和效益，努力实现以人为本、全面协调可持续的科学发展。加快经济发展方式转变是我国经济领域的一场深刻变革，关系改革开放和社会主义现代化建设全局。我国工人阶级和广大劳动群众是实现这场深刻变革的主力军，要积极为实现这项重大战略任务贡献力量。要全面贯彻落实尊重劳动、尊重知识、尊重人才、尊重创造的方针，充分发挥一切劳动者的首创精神，充分调动他们的积极性、主动性、创造性，最大限度地把他们的智慧和力量凝聚到推动科学发展上来。我国工人阶级和广大劳动群众要积极开展社会主义劳动竞赛，争当锐意改革创新的先锋，争当推动科学发展的楷模，把自己的创新潜能和创造活力充分发挥出来；要积极投身自主创新实践，围绕加快传统产业优化升级、推动战略性新兴产业发展，建设创新型国家，建设资源节约型、环境友好型社会等重大任务，深入开展技术革新和发明创造活动，立足本职岗位，丰富科技知识，提高劳动技能，争创一流业绩，把实现党和国家发展目标变成自己的自觉行动，为推动科学发展积极献计出力。<br />
<br />
　　第三，进一步保障劳动者权益，为促进社会和谐奠定坚实基础。实现好、维护好、发展好最广大人民根本利益是我们一切工作的出发点和落脚点。保障工人阶级和广大劳动群众经济、政治、文化、社会权益是我国社会主义制度的根本要求，是党和国家的神圣职责，也是发挥我国工人阶级和广大劳动群众积极性、主动性、创造性最重要最基础的工作。我们一定要适应改革开放和发展社会主义市场经济的新形势，从政治、经济、社会、法律、行政等各方面采取有力措施，保障广大劳动群众权益，促进社会公平正义。要健全以职工代表大会为基本形式的企事业单位民主管理制度、厂务公开制度，组织职工依法实行民主选举、民主决策、民主管理、民主监督，使广大劳动群众的知情权、参与权、表达权、监督权得到更充分更有效的保障。要切实实施积极的就业政策，创造更多就业岗位，促进充分就业，改善就业环境，提高就业质量，不断增加劳动者特别是一线劳动者劳动报酬。要切实完善社会保障体系，健全就业帮扶、生活救助、医疗互助、法律援助等帮扶制度，着重解决困难劳动群众生产生活问题，在经济发展的基础上不断提高广大劳动群众生活水平和质量，使他们不断享受到改革发展成果。要切实发展和谐劳动关系，建立健全劳动关系协调机制，完善劳动保护机制，让广大劳动群众实现体面劳动。要切实健全党和政府主导的维护群众权益机制，统筹协调各方面利益关系，想问题、作决策、定政策要充分考虑广大劳动群众利益和承受能力，认真解决广大劳动群众反映的热点难点问题。我国工人阶级和广大劳动群众要充分发扬识大体、顾大局的光荣传统，增强主人翁意识，坚决拥护党和政府关于改革发展的各项方针政策，正确认识和对待改革发展过程中利益关系和利益格局的调整，依法表达合理诉求，自觉维护社会和谐稳定。<br />
<br />
　　第四，进一步提高劳动者素质，为推动科学发展提供强有力的人力资源支持。劳动者素质对一个国家、一个民族的发展至关重要。当今世界的综合国力竞争，归根到底是劳动者素质的竞争。不断提高广大劳动群众的综合素质，是实现人的全面发展的必然要求，也是推动经济社会发展的重要保证。我们一定要深入实施科教兴国战略和人才强国战略，引导广大劳动者不断提高思想道德素质和科学文化素质、提高劳动能力和劳动水平，努力成为掌握新知识、新技能、新本领的知识型工人和一线创新人才，成为有理想、有道德、有文化、有纪律的社会主义劳动者，使科技进步和劳动者素质提高成为我国经济社会发展的重要推动力。要大力推进社会主义核心价值体系建设，引导我国工人阶级和广大劳动群众认真学习中国特色社会主义理论体系，坚定中国特色社会主义共同理想，弘扬以爱国主义为核心的民族精神和以改革创新为核心的时代精神，践行社会主义荣辱观，打牢为坚持和发展中国特色社会主义而共同奋斗的思想基础。要大力开展技能培训、转岗培训、创业能力培训，形成有利于劳动者学习成才的引导机制、培训机制、评价机制、激励机制。要大力开展多种形式的群众性精神文明创建活动，加强职业道德建设，积极发展丰富多彩、昂扬向上的企业文化、职工文化，不断满足广大劳动群众日益增长的精神文化需要。<br />
<br />
　　紧紧依靠和切实关心广大劳动群众，是坚持党的全心全意为人民服务的根本宗旨和贯彻党的群众路线最重要最根本的体现。各级党委和政府要始终坚持全心全意依靠工人阶级的根本方针，把广大劳动群众紧紧团结在党和政府周围，充分发挥他们的主力军作用。各级领导干部要增强对劳动群众的感情，密切同劳动群众的联系，深入劳动群众、关心劳动群众，倾听他们呼声，关心他们疾苦，为他们排忧解难，始终与劳动群众心连心。<br />
<br />
　　在庆祝今年“五一”国际劳动节、表彰全国劳动模范和先进工作者的喜庆日子里，我们也迎来了中华全国总工会成立85周年。在此，我向全国各级工会组织和广大工会干部，表示热烈的祝贺和诚挚的问候！工会组织是党和政府联系职工群众的桥梁和纽带，是国家政权的重要社会支柱，是职工利益的代表者和维护者。新形势下，各级工会组织一定要适应新形势新任务，紧紧围绕党和国家工作大局，全面履行各项职能，扩大工作覆盖面，增强组织凝聚力，诚心诚意为广大职工群众服务，主动维护广大职工包括农民工合法权益，充分发挥组织职工、引导职工、服务职工、维护职工合法权益的重要作用，把党和政府的关怀和温暖送到广大劳动群众心坎上，最广泛最充分地把广大劳动群众的智慧和力量凝聚到落实改革发展稳定的目标任务上来，不断开创工会工作新局面。<br />
<br />
　　我们要始终高举和平、发展、合作旗帜，加强同世界各国工人阶级和广大劳动群众的联系合作，扩大交往，增进友谊，为维护工人阶级和劳动群众权益，推动建设持久和平、共同繁荣的和谐世界作出应有的贡献。<br />
<br />
　　同志们，劳动是人类文明进步的源泉，劳动创造世界。在我们社会主义国家，一定要在全社会大力培育和弘扬劳动光荣、知识崇高、人才宝贵、创造伟大的时代新风，让全体人民特别是广大青少年都懂得并践行劳动最光荣、劳动者最伟大的真理。全面建设小康社会、加快推进社会主义现代化的伟大事业为英雄辈出提供了广阔舞台。我国工人阶级和广大劳动群众要更加紧密地团结起来，在党的坚强领导下，万众一心，开拓进取，继续为祖国、为人民、为民族建功立业，继续为全面建设小康社会、实现中华民族伟大复兴而不懈奋斗！</div>

							
							
							
							
