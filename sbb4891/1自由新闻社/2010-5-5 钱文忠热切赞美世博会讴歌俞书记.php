																											<div id="postmessage_598692" class="t_msgfont">我看上海世博会开幕式<br />
钱文忠&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp; 《光明日报》2010年5月2日 <a href="http://www.gmw.cn/01gmrb/2010-05/02/default.htm" target="_blank">http://www.gmw.cn/01gmrb/2010-05/02/default.htm</a><br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp; 2010年4月30日,举世瞩目的中国2010年上海世博会在黄浦江畔的世博园区,以一场令人叹为观止的美妙的开幕式正式揭开了帷幕。我有幸受邀出席了开幕式,成为这一旷世盛典的见证。这必将成为我生命中美好的记忆。<br />
　　现代传媒无远弗届。我相信,通过电视和网络,无数的观众都会有身临其境的感受。在接受新华社记者采访时,我这样说道:“如果说北京奥运会开幕式展示了中国的崛起和强盛,展示了中国文化的独特性,上海世博会的开幕式则更多的是显示了中国文化自信的另一面:包容、接纳、欢乐、分享。”“分享、欢乐,相信全世界都从中感受到了中国的姿态。上海世博会的舞台也是全世界共同的舞台。”<br />
　　上海世博会开幕式的理念是“简洁、唯美”,强调艺术性和仪式感的圆融统一、高科技与原生态的交相辉映,中国文化和西方艺术获得了完美的交融和和谐的体现。我用了“绚烂归于平淡”来概括开幕式给我的印象。“绚烂”无需辞费。这里所讲的“平淡”,当然绝对不是没有特色之谓,而是以一份平常心,展示出今天的中国在首次于发展中国家举办的世博会这样的全球盛会面前,所秉持的那一份淡定与从容。这才是真正的自信。<br />
　　然而,让我感触最深的却是三个也许是小小的细节。开幕式的主持者,中共中央政治局委员、上海市委书记俞正声“恭请”胡锦涛主席宣布世博会开幕。在重大而正式的官方典礼中,这是我第一次听见“恭请”这样的用词。我以为,短短的一声“恭请”,传达出的不仅是庄重典雅,更是对中华传统文化的深沉体认。用词的准确考究,难道不正是营造仪式感的题中应有之义吗?<br />
　　来自青海玉树地震灾区的两位藏族儿童也出现在了开幕式的舞台上。和以往不同的是,他们的出现并没有带来哀伤悲戚的气氛。他们和不同国家、不同肤色的孩子牵手相拥,在不同国家、不同肤色的手臂的举托下,他们摆脱了灾难的阴霾,幸福地欢笑。人类在面对各种问题、各种灾难的时候,更需要的难道不正是坚强乐观、守望相助吗?<br />
　　转场以后的烟花表演场面恢弘、运思巧妙、绚丽斑斓。事先不少人持有这样的疑问:比北京奥运会开幕式规模更大的烟花表演固然极具视觉震撼力,可是,又如何才能处理好和上海世博会的主要关注点低碳、减排之间的冲突与矛盾呢?就我在现场的感受而言,几乎没有闻到燃放烟花通常必定会产生出来的刺鼻气味。这就说明,按照最大限度地降低烟气、减少残渣的要求特殊设计制造的上海世博会烟花,也取得了非凡的成功。同时,这难道不也正是对本届世博会主题“城市,让生活更美好”的最佳实践和演绎吗?<br />
　　这是一场成功的开幕式,这是一场难忘的开幕式。</div>

							
							
							
							
