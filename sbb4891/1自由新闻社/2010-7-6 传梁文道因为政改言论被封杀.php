																											<div id="postmessage_723734" class="t_msgfont">梁文道：香港政改再思考<br />
2010年07月04日07:59时代周报梁文道我要评论(47) <br />
<br />
<br />
梁文道 凤凰卫视主持人<br />
<br />
每当有关香港政制改革的新闻传入大陆，就总会有人在网上留言，说“香港人怎么以前不向英国争取民主，现在回归了才来争民主呢”。每次看到这种言论，我都不知该说什么才好。<br />
<br />
首先，它的前提是错的。香港人的民主运动成形于上世纪80年代；早在英殖民末期，今天香港台面上的“民主派”就已经是当时十分活跃的政治人物了。所以说出这种话的大陆网民恐怕不太了解香港。其次，这句话把重点放在了向谁争取民主这个问题上面，而非争取民主本身；似乎民主运动本身没有内在的道德价值，是非对错全看你是在向什么人争取民主。向英国人要民主就对，跟自己人计较就是居心不良，这难道不是一种价值的移位和错置吗？<br />
<br />
说来令人神伤。1997年以前，有些人担心未来的政治局面会变得更加闭锁；但也有一部分人深信在摆脱了150年的外来殖民统治之后，我们当家作主了，中国人自己施行人人平等的民主政治，难道不是天经地义、顺理成章的一件事吗？那为什么香港政制改革会演变成后来那种争论不断的艰难局面？又为什么这个过程反而会引来部分大陆同胞的非议呢？这事错综复杂，千头万绪，我们不妨从最惹火最为人关注的立法会“功能组别”说起。所谓“功能组别”，就是香港立法会内那三十席代表不同社会界别的席次，例如“工程界”、“金融界”、 “会计界”、“地产界”、“商界”、“金融服务界”、“工业界”、“纺织界”和“饮食界”。这三十个界的代表和地区直选产生的三十位议员享有同等权力；但一位地区议员代表背后是数以万计的普通百姓，而一位功能界别议员只要搞定业内几千甚至一百多票就行了。也就是说，他们的权力来源是完全不同的，而且非常不均衡。<br />
<br />
不止如此，某些界别的选民基础还不是个人票，而是公司票。举个简单的例子，一位地产经纪虽然是地产业内的一员，但他其实并没有选举地产界议员的权利；真正有权投票选代表的是地产公司。比如说李嘉诚旗下的“长江实业”，它的董事局才有资格投下这神圣的一票。一个公司一张票，而非一个员工一张票；公司投票给谁，员工无权过问，老板说了算。<br />
<br />
这三十种界别到底是在什么选择基础上区分的呢？为什么有些行业能够形成功能界别有些不能？很抱歉，从来没有人说得清楚。“商界”又为何会有两个代表？同样也没有人能提供合理的解释。至于“工业”和“纺织”，“金融”与“金融服务”为什么会各自分开；而“教育”又不按大、中、小学划成三张票；那就更没办法说明了。我们只知道这些界别很容易形成网络，被少数人操控。比方说“会计”，会计这一行的主要客户就是香港那几十家大财团，所以“会计界”的代表在议会表决以及做任何政治决定之前都不能不看后者的脸色。又由于那些财团的生意够大，旗下公司够多，往往横跨多个领域，在几个界别里头都有投票权，所以有时候一个家族就能影响好几个界别的选情，形成一张巨大的权力网络，一般市民根本无力抗衡。以制度方式保证资本家的政治权力，放眼全球，也就只有香港了。<br />
<br />
与此同时，立法会内还有分组点票的机制。任何一项议案的通过，都不是议会全体多数票决定得了的，它必须分别在“功能界别”与“地区直选”这两大块里各自得到半数以上的支持。它的毛病很明显，但我们不妨再举一个例子来演示一下。比如说香港买卖房产的方式一向为人诟病，广告宣传夸张不实就别说了，很多买家甚至到了付交订款那一刻都还不晓得自己买的房子究竟有多少实用面积。假设今天政府提出一项保护消费者的法案，意图杜绝这些问题。再假设全港市民直选出来的那三十位议员也都很支持这个有利于百姓的法案，全票赞成。可是到了“功能组别”那一块，只要有十五位议员反对，这项深孚民意的议案就会被他们否决掉了。换句话说，全香港数百万人的意见在议会里头可以很轻易地被十五个只代表自己业界的议员推翻，而那十五个议员在这件事上很可能就是几个超级财阀的代言人而已。什么叫做以一敌万？这就是最好的示范了。<br />
<br />
为什么香港会有一套这么古怪又这么不平等的议会制度呢？这还得从历史说起。除了香港，历史上只有两个政权实行过类似的议会设计，那就是意大利的墨索里尼和西班牙的佛朗哥。所以我们可以大胆地说，这种深受法西斯独裁政权与殖民帝国欢迎的制度。理由很简单，统治者不用那么麻烦地去讨绝大多数普通公民的欢心，它只要笼络几千个甚至几百个有财有势的人，就能牢牢掌握局面，方便得紧。而那些有财富有地位的精英本来可能是当政者的潜在挑战力量，这套优惠他们的办法可以把他们变成自己的同盟，巩固自己统治的基础，然后形成一种名正言顺的 “官商勾结、精英共治”。<br />
<br />
港英殖民政府借用了这种源起于法西斯主义的策略，于是可以安心地把香港打造成一个它心目中的“经济城市”，表面上说是为了资本主义发展所以才把那么多的权益交给少数权贵；实际上却是要方便当年想在香港大赚其钱的英商，以及曾经对其非常效忠的后起华人富豪。<br />
<br />
这便是今日香港政改争论的焦点了。立法会“功能界别”的存在不仅不合理，而且还违反了《基本法》所规定的“立法会最终达致全面普选”的目标。香港回归了，港人当然有权期盼它的改变，当然有权甩脱这套加诸我们身上的“殖民余孽”，1997年前，我们一度听说这个目标将会在2008年实现的消息；然而到了今天，它却依然是一个有待奋斗的远景。那些批评香港人“以前不向港英争取民主，回归才开始说要民主”的大陆同胞们，与其批评我们，何不帮忙想想，为什么一套港英年代的不民主制度居然可以拖到回归十多年后的今天呢？</div>

							
							
							
							
