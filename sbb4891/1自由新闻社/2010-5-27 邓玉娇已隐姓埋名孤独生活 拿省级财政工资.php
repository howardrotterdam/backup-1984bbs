<br />
　　<strong>邓玉娇：隐姓埋名只为安静活着</strong><br />
<br />
　　邓玉娇，一名湖北省巴东县野三关镇雄风宾馆梦幻城女服务员。因为去年5月“刺死官员案”，轰动全国而被网民们称为“2009年中国第一烈女”。<br />
<br />
　　一年过去了，目前的邓玉娇可安好？此事在当地到底产生了哪些影响？连日来，时代周报记者前往恩施，对这一曾经轰动全国的事件进行了一次回访。<br />
<br />
　　本报记者 姚海鹰 实习记者 何光伟 发自恩施<br />
<br />
　　2009年5月10日，邓玉娇案发。在网络民意的推动之下，邓玉娇最终被认定防卫过当有罪，但免予刑事处罚，邓玉娇慢慢淡出公众视线。<br />
<br />
　　但10个月后，邓玉娇因两会期间的“录音笔事件”，再次引起公众关注。<br />
<br />
　　从案发至今已有一年。在这一个轮回的春夏秋冬中，邓玉娇的个人生活到底怎样？该事件对恩施当地的社会生态有哪些影响和改变？<br />
<br />
　　邓玉娇认为她现在的生活来之不易，她需要这种安静的生活。<br />
<br />
　　困扰邓玉娇多年的失眠现在好多了，为此，恩施电视台专门购买了跑步机，这对她治疗失眠的帮助很大，她正准备去学驾驶。<br />
<br />
　　现在，邓玉娇最需要面对的就是孤独。没人敢跟她讲话，她一个人独来独往。<br />
<br />
　　事实上，邓玉娇案至今了犹未了。在恩施州，该案至今阴云未散，是当地官员不敢触碰的禁区，谈之色变。<br />
<br />
　　<strong>神秘的张姓女孩</strong><br />
<br />
　　5月的初夏，正是湖北恩施的多雨时节，被雨后高山云雾笼罩着的山城恩施，一切都显得那么模糊和吊诡。<br />
<br />
　　邓玉娇来到这里已经一年，她现在对外已不使用“邓玉娇”这个名字了，大家都称她“小张”。<br />
<br />
　　电视台办公室的工作人员称，从来就没有邓玉娇这么一个人。<br />
<br />
　　邓俨然成了一个神秘人物，她的一名同事称：“听说小张经常到健身房馆去，每次都是一个人。”<br />
<br />
　　为能见到邓玉娇，时代周报记者多处打听，但始终未能如愿。<br />
<br />
　　几经辗转，时代周报记者18日到野三关后才联系上邓玉娇，她和记者交流时声音低缓，言语间不时流露出戒备之意。简短几句，就能明显感到她是个忧郁而腼腆的人。<br />
<br />
　　但邓玉娇也并非拒人于千里之外，她直接说“有什么问题你就问吧”，但回答时，她却要思考几分钟后才能作答，而且非常简短。显然，她的自我保护意识非常强。<br />
<br />
　　邓玉娇告诉时代周报记者：“我只是想安静地生活，人的承受能力是有限的，你们的关心，让我无法摆脱那些让我难受的事情。”<br />
<br />
　　过去的事情对这位1987年出生的女孩来说，无疑是一个残酷的噩梦。邓玉娇希望能忘掉过去的一切，她目前最需要的就是安静的生活。<br />
<br />
　　邓玉娇说：“可能大家都不知道我现在的情况，我挺好的，精神状况和工作都很好，我会努力工作，不让关心我的人们失望。”<br />
<br />
　　就在16日下午，邓玉娇穿一套kappa牌黑色运动服和白色运动鞋走在恩施州委幼儿园对面的街上，她的头发挽到脑后，显得特别清爽，在街上边走边看。<br />
<br />
　　走到一家商店门口，她随便瞄几眼就离开了。她仿佛没有任何购物的目标，也不是要买东西，就是随便逛逛，一个女孩子常见的那种逛街爱好。<br />
<br />
　　时间已经过去一年了，“邓玉娇”三字，似乎已成为一个逐渐被人淡忘的符号。<br />
<br />
　　“邓玉娇被安置以后，外界就很难得跟她接触了。”恩施州广播电视局一官员直言不讳：“严格地说，已经没邓玉娇这个人了。”<br />
<br />
　　就连同事也认为邓玉娇并没有跟他们亲口承认过她就是邓玉娇，他们也只能说每天身边生活着一个和邓玉娇长得很像的人。<br />
<br />
　　<strong>电视台里的孤独背影</strong><br />
<br />
　　在恩施人眼里，邓玉娇能进入恩施州电视台工作，完全是因祸得福。<br />
<br />
　　一位当地人说：“这是个好单位，能在这里工作的人，大都有点关系或后台。”<br />
<br />
　　在恩施州委斜对门的恩施电视台，就是邓玉娇上班的地方。从去年7月至今，她已经在恩施电视台的舞阳微波站工作10个月了。<br />
<br />
　　邓玉娇每天早晨从恩施州委大院走到马路对面的恩施电视台上班，下班后再走回恩施州委大院的住处。<br />
<br />
　　眼前的生活和工作她已习以为常，邓玉娇每天都基本过着从宿舍到单位的两点一线式生活。<br />
<br />
　　邓玉娇从农村来到了城市，她对目前的工作表示满意：“我现在工作很顺心，我会记住和感谢关心我的每一个人。”<br />
<br />
　　邓玉娇在18日晚上加夜班。她告诉时代周报记者：“我们这里人人平等，大家都会加夜班的。我今晚加夜班，明天白天就可以休息。”<br />
<br />
　　知情者告诉时代周报记者，在电视台里邓玉娇拿的是省级财政工资，与微波站签了的是无固定期限合同。就是说，邓玉娇后顾无忧，只要愿意可以在此干到退休为止。<br />
<br />
　　记者了解到，恩施电视台的职工分为在编和聘用两种，只有在编职工才能拿财政工资享受规定的福利，而聘用的职工就不能享有这些待遇。<br />
<br />
　　时代周报获悉，邓玉娇当前的这份工作，主要来自湖北省的意愿，一位知情者披露说：“恩施州当地政府当时并没有这个想法，是更高层领导的意见。”<br />
<br />
　　无论如何，这都是一个比较令人满意的结局，政府对邓玉娇的这种安置，在恩施坊间也获得了普通民众的认同。<br />
<br />
　　谈及邓玉娇，恩施的一位出租车司机表示：“怎么说她都是受害者。无论如何，我们都希望她过得好。”<br />
<br />
　　邓玉娇还告诉记者：“电视台的工作很顺心，我现在的生活和工作来之不易，我这一生，终究还得靠我自己努力。毕竟大家帮得了我一时，帮不了我一世。”<br />
<br />
　　她言辞诚恳，但充满沧桑。<br />
<br />
　　现在，邓玉娇最需要面对的就是―孤独。<br />
<br />
　　由于种种原因，很多人不敢跟邓玉娇讲话。加之她以往的朋友都不在身边，邓玉娇很难找人说知心话。在同事眼中，她就是默默地，每天一个人独来独往。<br />
<br />
　　“如同拉了一张网，她的交际圈子太窄了。”邓玉娇的一位同事说，“她时常沉默，能感觉到她很孤独。”<br />
<br />
　　邓玉娇的一位朋友告诉记者：“所有人都对她敬而远之，谁也不敢拿自己的生活开玩笑。”<br />
<br />
　　这位朋友认为： “邓玉娇只有离开这个地方，才能真正重新开始新的生活。”<br />
<br />
　　还有一个重要的问题就是，邓玉娇今年已经23岁了。他担心邓玉娇在恩施很难解决婚恋问题：“离开恩施也许能好一些，这很现实。”<br />
<br />
　　记者了解到，邓玉娇近期正准备学驾驶，电视台决定在今年9月送她到武汉读中专，弥补电视台工作上的专业知识。<br />
<br />
　　邓玉娇的失眠比以往好多了，为此，电视台专门为她购买了跑步机，这对她治疗失眠的帮助很大。<br />
<br />
　　“她的失眠症基本好了，人比以前鲜活多了。”她的一位同事说。<br />
<br />
　　<strong>　不为人知的幕后</strong><br />
<br />
　　“邓玉娇能够过上平静的生活，最终还要归功于网络的影响力。”恩施本地媒体的一名记者表示。<br />
<br />
　　诚然，邓玉娇得以保全清白的同时又免却牢狱之苦，不仅得益于当时她手中那把刀这一“硬件”，更得益于网民的舌头这一“软件”。<br />
<br />
　　“如果没有网友、媒体的帮助，也不会有邓玉娇的今天。”邓玉娇的爷爷邓正兰当时就认为网络对整个事件所起的作用巨大。<br />
<br />
　　“媒体对邓玉娇事件及时的报道，网络的强力扩散，是地方政府反应迅速的原因之一。”上述记者称，“至少这件事情，是因为网络的传播才引起高层的关注，不管是从恩施州还是到省里。”<br />
<br />
　　时代周报记者了解到，去年5月19日，正在重庆出差的湖北省委一位领导高度关注邓玉娇案，他在登机返鄂前就电令湖北省政法委汇报该案情况，并要求在他下飞机时，就能看到邓玉娇案的全部材料。<br />
<br />
　　就在湖北省委过问之后，湖北省公安厅主管刑侦的副厅长尚武在去年5月20日亲自率团抵达巴东坐镇调查，而恩施州公安局也直接介入办案，就此该案发生逆转。<br />
<br />
　　“那几天，路似乎变窄了，常堵车。”野三关镇的一位麻木司机回忆：“镇上来往的车辆明显多了起来，还有到雄风宾馆的人也很多。”<br />
<br />
　　“各级官员在一天内进出镇政府大门的次数非常频繁。”在野三关镇一个名叫“喝二两”的小酒馆里，一位食客向时代周报记者这样形容地方政府因邓玉娇案反应的迅速。<br />
<br />
　　邓玉娇、官员、网民，迅速通过网络形成了互动。网民们的舆论形成了强大话语声势，在帮助邓玉娇的同时，却也给相关部门的工作带来了压力。<br />
<br />
　　但值得肯定的是，湖北网宣部门在网络舆论中顺势而为，根据省领导指示真诚认真面对，直到该事件被以最快速度平息。<br />
<br />
　　时代周报记者获知，湖北省委宣传部一副部长在某次内部会议的讲话中透露，仅当时参与讨论“邓玉娇案”的QQ群，就有近三万个之多。<br />
<br />
　　这对湖北的新闻应急管理和网络舆论引导取得新成效，湖北网宣部门在邓玉娇案等突发事件新闻的应急工作中，因表现突出获得嘉许。<br />
<br />
　　一位官员表示：邓玉娇案件有很多值得反思的地方，首先就是如何正确面对突发事件和处理突发事件。<br />
<br />
　　他评价：“无论发生什么事情，政府部门只要拿出诚意真诚面对，就能很好解决。”<br />
<br />
　　<strong>　阴霾并未散去</strong><br />
<br />
　　邓玉娇事件严重冲击了巴东官场，而野三关镇政府首当其冲承担责任：镇派出所所长谭静被免职，调往巴东县公安局任一名普通民警；镇党委书记谭昱调往巴东县政协；镇纪委书记蚱舳调往水布垭镇当宣传委员。<br />
<br />
　　就连巴东县公安局局长杨立勇，也为“擅自”接受媒体采访埋单，他被免职后调离巴东进了恩施州人大。<br />
<br />
　　尽管巴东相当多的政界人士认为，杨立勇当初通报案情、接受媒体采访并没有错。但这名巴东唯一法律专业毕业的公安局长，以后能否东山再起就是个未知数了。<br />
<br />
　　地方政府因邓玉娇事件付出巨大行政代价的同时，当地舆论环境也同样遭受批评。<br />
<br />
　　巴东官方当时关闭言论渠道的阴影，不仅让当地官员对邓玉娇事件噤若寒蝉，这种效应也传染到了恩施州。<br />
<br />
　　虽然事情已过去一年，但邓玉娇事件仍然是当地政府官员们不敢去触碰的禁区。<br />
<br />
　　恩施州的一位官员告诉时代周报记者：“你想采访邓玉娇，除非她自己愿意说。没有州里领导同意，其他人不会跟你多说什么。”<br />
<br />
　　时代周报记者在恩施接触过几位熟悉邓玉娇的官员、记者，他们都以不方便透露或不知情为由，基本不愿谈论“邓玉娇”三个字。<br />
<br />
　　记者了解，为应对邓玉娇事件，花去了2009年巴东县政府数额惊人的财政收入。“花这样的钱是小事”，巴东县的官员们更担心引起其他事故。<br />
<br />
　　直到目前，当地政府对邓玉娇高度紧张的神经仍未放松。<br />
<br />
　　记者了解到，当时政府除答应为邓玉娇安排工作外，还承诺给她父母解决一个客运线路营运证，以保障邓玉娇全家的生计问题。<br />
<br />
　　“但是现在他们耍赖。”邓玉娇的上述朋友称，“当初政府的有些承诺现在并没兑现，这是没有办法的事情。”<br />
<br />
　　诸如解决客运线路营运证这类的空头支票，“让邓玉娇和她的家人觉得无助。”他表示，“这是政府的伤疤，他们当然不愿意揭了。”<br />
<br />
　　从野三关镇到巴东县乃至恩施州，不仅是下面的官员不敢公开谈论邓玉娇。即便是恩施州的高层，也同样觉得不合时宜。<br />
<br />
　　5月21日下午，记者两次拨通恩施土家族苗族自治州州长杨天然的手机，第一次通话持续30秒，但始终未听到对方讲话。<br />
<br />
　　8分钟后再次通话，杨天然闻听是采访关于邓玉娇的事情，他说了一句话：“瞎搞吧，你这个时候，还问我这个做什么？”随即挂断电话。<br />
<br />
　　<strong>　　倒闭的雄风宾馆</strong><br />
<br />
　　本报记者 姚海鹰 实习记者 何光伟 发自巴东<br />
<br />
　　一年前震惊全国的邓玉娇事件，就发生在巴东县野三关的雄风宾馆。<br />
<br />
　　因为这起命案，雄风宾馆声名大噪。这起命案改变了农村姑娘邓玉娇的生活轨迹，也结束了雄风宾馆“雄风”的命运。<br />
<br />
　　5月18日上午，记者来到这个传说中的是非之地。该宾馆地处鄂西山原的野三关，雨水过后，被笼罩在群山环抱的云雾之中。<br />
<br />
　　除雄风宾馆外，野三关镇依然“开市照常”，镇上往来的人群和车辆交织在雨中。似乎这里什么都没发生过，一切都是那么从容。<br />
<br />
　　而在该镇家喻户晓的雄风宾馆，早已衰败不堪。红色的墙壁已被“湖北三峡酒业”的巨幅广告牌遮盖，一长排房子只留了两个小门。<br />
<br />
　　一个蓬头垢面的乞讨者在雨中走过宾馆门口，他张望着路上过往的行人，让人不觉顿生悲凉之感。<br />
<br />
　　“雄风宾馆是野三关最大的娱乐城，有餐饮、洗浴、住宿、美容等项目，生意特好。”提及雄风宾馆，附近的居民们仍然不忘美言一番，昔日的繁华对照今日的沉寂让人感怀。<br />
<br />
　　居民们说，雄风宾馆就是在邓玉娇案期间关门的。至于关门的原因，他们的看法产生了分歧。<br />
<br />
　　“是被迫关门，老板怎么舍得自己关门呢？”<br />
<br />
　　“死过人，这里就是凶宅，不适合住人，要犯忌的。”<br />
<br />
　　居民们的议论，听起来似乎都有道理。对于一年前的往事，他们谈起来仍然津津乐道。<br />
<br />
　　其实，雄风宾馆的老板赵雄在邓玉娇事件期间亦被刑拘，后来患病取保候审。<br />
<br />
　　住在湖北三峡酒业公司后面的一位居民告诉时代周报记者：“雄风宾馆的房子是酒厂的。赵雄以前资助过学生读书，听说他人还行。”<br />
<br />
　　“邓玉娇是野三关的人，即便她现在隐姓埋名了，我们也知道。” 居民们谈到邓玉娇时，显得非常亲切，就像谈自己的一个亲戚或朋友。<br />
<br />
　　居民们还说：“很多外地人都来看这个宾馆，就连有些领导路过野三关，也会下车瞧瞧雄风宾馆，当时都成野三关的景点了。”<br />
<br />
　　至于雄风宾馆这座房子，当地人认为被拆掉才是其最终的命运。<br />
<br />
　　因为，按照当地风俗，凶宅不适合住人，更不适合做生意。</div>

							
							
							
							
