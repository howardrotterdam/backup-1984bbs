													<h2>论国民党的“生殖器串联”</h2>
						
						
																											<div id="postmessage_495465" class="t_msgfont">论国民党的“生殖器串联”<br />
<br />
-----生殖器引发的杂感<br />
<br />
李敖<br />
<br />
1983-1987年<br />
<br />
&nbsp; &nbsp; 《前进》这些小朋友，又做教人皱眉的事了。昨天看到《前进广场》第二期，把我的文章――《他们的恭维是我们的耻辱》――偷偷删改如下：国民党不是政治学上的一个政党，反倒是民族学上、人类学上的一个典型内婚制的大家族，一个靠裙带关系串连起来的有刀有枪有镇暴车的大家族。<br />
<br />
　　我的原稿明明是“一个靠生殖器串连起来的有刀有枪有镇暴车的大家族”，他们却硬割掉我的硬梆梆的“生殖器”，而改成软扒扒的“裙带关系”，这种偷天换日，是违背当初约稿的协议的。我本想一怒而“罢工”，继而又想：一、在这岛上“罢工”，是国民党不准的，按照“戒严法”第十一条第一项第三款：“对于人民罢市、罢工、罢课及其他罢业，得禁止及强制其回复原状”。可见“罢工”是违反“戒严法”的，不是好玩的；二、丘垂亮最近发表文章说：“李敖的文采没有一个人不崇拜，但是他的专制独裁的性格，恐怕连林正杰也不能忍受。”此番我若一怒而去，海外学人也者，必然又会说我“专制独裁”了林正杰。所以，我决定还是按住性子继续“帝国主义”下去，虽然这种种“帝国主义”，已经“日薄崦嵫”；和在香港的大英帝国一模一样。<br />
<br />
　　虽然如此，我想我还是要表示一点抗议，我决定改变一下写作的方式，我决定暂时不写“重头文章”给《前进》了，我决定改写一点杂感性的东西。这种在我内心里“降低关系”（downgrade）的“惩罚”，也许《前进》小朋友根本感觉不出来（告诉你：他们常常是很粗心的），甚至认为改成杂感也不错。但是不论他们有否感觉或警觉，我决心要“惩罚”一下了。 <br />
<br />
一、为什么不用“生殖器”？<br />
<br />
　　《前进》小朋友偷偷删改“生殖器”为“裙带关系”，其中一个原因是中了“性禁忌”的毒，这种中毒的特色，是一种“反对‘性’的（anti－sexual）现象。从历史角度来看，中国历史上，反对…性”的现象，至少在表面上占了上风，所以规律、约束，乃至压抑“性”的理论与事实，总是层出不穷。而经典、当局、理学、教条、迷信、教育、舆论等所层层使出来的劲儿，大多是在“解淫剂”（antiaphrodisiacs）上面下功夫，在这种层层“解淫”之下，善于掩耳盗铃的人们，总以为“没有‘性’的问题”，因为“中国是礼义之邦”！流风所及，一涉到“性”的问题，大家就立刻摆下面孔，道貌岸然地缄口不言，或声色俱厉地发出道德的谴责。因此，“性”的问题，终于沦为一个“地下的”问题。这样重大的间题，居然千百年不见天日，怎么能不发霉呢？<br />
<br />
在发霉的情况下，竟使我们对真正因生殖器而来的现象，都不敢一语道破，这就未免太失真了。在《史记》里，有公然记录“大阴人”的故事，而不加删改；在《战国策》里，有公然记录“以其髀加妾之身”的故事，也不加删改……可见我们的老祖宗并没像我们今天这样假道学。今天，以前进号召的我们，实在该努力反璞归真、实在该冲决网罗，建立我们的新词汇！ <br />
<br />
二、国民党的“生殖器串连”<br />
<br />
　　仔细研究中国民族的历史，会令人惊讶地发现，直接由于生殖器的因素，直接影响了历史、改写了历史的，例证又多，又层出不究。夏桀是以“荧惑女宠”妹喜亡了国的，商纣是以“荧惑女宠”妲己亡了国的，生殖器的原因使人亡国，不能说不重要。赵婴的私通，引出赵氏孤儿；齐庄公的私通，引出臣弑其君，生殖器的原因造成政变，不能说不重要。吕不韦的奇货可居，祸延秦皇显考；吕后的人彘奇妒，祸延刘家命脉；唐高宗的倒扒一灰，祸延武后临朝；杨贵妃的顺水人情，祸延安史之乱，生殖器的原因闹出君权争夺，不能说不重要。白登的美女图片，可以使匈奴不打汉家；汉家的美女自卑，可以使汉家要打匈奴；昭君出塞，香妃入关，生殖器的原因牵动战争和平大计，不能说不重要。齐襄公乱伦，出来了毋忘在莒；陈后主好色，出来了井底游魂；慕容熙的跳步送亡妻，出来了回不去；花蕊夫人的被劫入宫，出来了送子张仙；咸丰的天地一家春，出来了祸国殃民四十七年的西大后。生殖器的原因导致国破家亡，不能说不重要。<br />
<br />
　　降至国民党抢到天下后，国民党形而上固然靠美丽的谎言统治，形而下其实是靠“生殖器串连”统治，所以我才指出：“国民党根本不是政治学上的一个政党，反倒是民族学上、人类学上的一个典型内婚制的大家族，一个靠生殖器串连出来的有刀有枪有镇暴车的大家族。”正因为是由生殖器串连起来的一家人，所以，我们可以随处看到他们瓜蔓出来的血亲和姻亲，例如张继正是张群之子、陈履安是陈诚之子、王纪五是王世杰之子、郑心雄是郑介民之子、沈君山是沈宗瀚之子、夏公权是黄少谷之婿、马树礼是顾祝同之婿、李元簇是徐世贤之婿、丁善理是费骅之婿、汪敬煦是何应钦之侄女婿……这样子串连来串连去的今古奇观，便是国民党“吾乓灰怨嶂”的床上功夫，而直接影响了历史、改写了历史。我们这些小百姓。若不这样单刀直入去了解国民党，我们是永远不会了解国民党的！<br />
<br />
生殖器关系的“积极好处”和“消极好处’<br />
<br />
国民党集团基本上是生殖器串连的集团，进入这“0与一之间”以后，不论湿的干的，都可利益均沾或分沾，反正有好处就是了。所谓好处，可分“积极好处”和“消极好处”二类。“积极好处”是可当官、可掌权、可分肥、可发财、可长袖善舞、可得君行道、可这可那，说不胜说，并且也不必说，因为人人都知道；但是“消极好处”却理未易明，须加解说。所谓“消极好处”，就是人在倒霉之时，因有生殖器关系，纵使倒霉，也另授优待、也比另从倒霉倒得程度轻之谓。例如王正谊者，贪污，已也，倒霉入狱，但因他是蒋经国表弟，在狱为日星，最后判得不重，逍遥而出；又如丘延亮者，叛乱犯也，倒霉入狱，但因他是蒋纬国小舅子，在狱延其亮，最后也判得不重，神气而出…… <br />
<br />
　　当然“消极好处”的获得，端赖生殖器关系亲疏远近而定，无法强求。孟绝子被捕，他的爸爸与姐夫都是少将，国民党少将多如野狗，自不足以沾多少光，但少将毕竟多少也搭上一点线――照刘玉章“将军是鸡巴，将军手下是鸡巴毛”的逻辑，少将与蒋家纵无生殖器关系，至少也有鸡巴毛关系，所以孟绝子在倒霉入狱时，终得“消极好处”，少挨了几个耳括子――别以为在狱中少挨几个耳括子不算什么，别忘了莎士比亚剧中倒霉之君以江山易马的故事吧，刑求之下、一无所恃之时，“消极好处”比“积极好处”还实惠得多呢！</div>

							
							
							
							
