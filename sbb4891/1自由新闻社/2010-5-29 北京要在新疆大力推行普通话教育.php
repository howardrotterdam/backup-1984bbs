<br />
<br />
　　<strong>为解决新疆日益严重的民族矛盾，北京除了加大对新疆的经济投入外，还大力推行对新疆年轻维吾尔人的汉语普通话教育。</strong><br />
<br />
　　<img src="http://yopic.us/images/1kwk.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　<font color="DarkRed">北京要求，到2015年，新疆所有中小学都必须完成维汉双语教育。</font><br />
<br />
　　新华社报道，周四（5月27日）新疆自治区政府举行的高层会议出台了一项推广普通话的具体方案。<br />
<br />
　　该方案要求，到2012年，全新疆所有幼儿园都应当普及维语和普通话的双语教育；而中小学校应当在2015年达到这一目标。<br />
<br />
　　此外，到2020年，所有的学生必须会说流利的维语和汉语。<br />
<br />
　　当地一名官员被引述说，在上周的中央新疆工作会议之后，教育部宣布将在未来五年内为发展新疆的教育额外拨款800亿元人民币。<br />
<br />
　　最近一些年，新疆每年从中央政府获得300亿元人民币的财政拨款。<br />
<br />
　　中央民族大学民族教育研究所的新疆问题学者藤星教授说，在新疆大力推行普通话是北京政府为保证新疆的长治久安所采取的重要步骤。<br />
<br />
　　他说，在就业市场竞争日趋激烈的情况下，掌握汉语也有助于提高年轻维吾尔人在求职方面的竞争力。<br />
<br />
　　过去两年，新疆不断爆发民族骚乱。去年7月乌鲁木齐发生的维汉之间的暴力冲突导致近两百人死亡，数千人受伤。<br />
<br />
　　北京政府担心，一个偏远落后的新疆将成为发展极端主义的温床。它决心通过大力投资新疆的经济建设和推行普通话教育，以加强对新疆的进一步控制。<br />
