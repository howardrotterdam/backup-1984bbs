还有香港还特别指出的是某地段的不分割份数,而广州的说的是很笼统的2/3.<br />
意见中为什么是用2/3而不是用百分比?你转换下到百分比是多少.2和3比是占了大多数.但是转成百分比的话这个比值就很难看了.<br />
广州的规定和香港定的看来是相似,但是认真比较还是有很大的不同.<br />
<br />
还有你说的全部同意无异于一票否决.这个说法完全是偏向了开发商.有谁卖房子时会问附近的99户人同不同意他卖?有谁买房子时会问那房子附近的住户同不同意他买?卖买完全是卖买双方1对1的事.开发商要买100户的房子还是100个1对1的关系.任何1户的买卖只是他和开发商的事.不买够100户就无法开发这个是开发商的事.<br />
如果有100户人100%同意要出售他们的房子然后找开发商买.开发商不愿意.那是不是可以把开发商告上法庭.要法庭强制开发商买下来.<br />
经常听到很多专家说实行一票反对制不行.但是这些专家从来不说联合国安理会实行的就是5个常任理事国的一票反对制.<br />
广州的规定是在为了防止钉子户的出现而影响旧城改造的背景下出台的.就知是会偏向开发商.怎么可能会出现不拆的事情.就好像广州的垃圾焚烧一样,你不同意是吧?那我就先不烧.等到垃圾把你家围起来的时候就是你求着他要烧了.到时也是民主决策出来的.<br />
