													<h2>影帝三十五中课堂笔记流出</h2>
						
						
																											<div id="postmessage_277938" class="t_msgfont">（前面文字报道删除）<br />
<br />
<br />
【听课点评】<br />
<br />
温家宝说，为了使会议开得活泼一些，在大家发言之前，我想对上午5堂课做个点评，互相切磋。如果说的不对，请你们批评。<br />
<br />
1 数学课<br />
<br />
内容单薄可加量<br />
<br />
第一堂听的是数学课，主要是讲三角形全等的判定。<br />
<br />
老师在讲这个内容的时候，用的是启发式教学，也就是启发同学来回答。老师在问到学生如何丈量夹角的度数时，同学们回答了好几种，比如量角器、圆规、尺子。我觉得这堂课贯穿着不仅要使学生懂得知识还要学会应用的理念。最后老师提出两边夹一角的判定方案，也就是SAS判定方案，并且举出两个实例让学生思考。两个例子都是联系实际教学生解决问题。<br />
<br />
所以这堂数学课概念清楚、启发教育、教会工具、联系实际，说明我们数学的教学方法有很大的改进。<br />
<br />
总的看这堂课是讲得好的，但是我也提一点不成熟的意见：我觉得40分钟的课包容的量还可以大一点，就是说，一堂课只教会学生三角形全等判定，内容显得单薄了一些，还可以再增加一点内容。<br />
<br />
2 语文课<br />
<br />
课文作者未作介绍<br />
<br />
第二堂听的是语文课。老师讲的是《芦花荡》，在座的可能有不少老师讲过，我过去也读过，但今天和学生们一起读，觉得别有一番新意。缺点是开始没把作者的简要情况给同学们介绍。<br />
<br />
既然是讲《芦花荡》，作者又是孙犁，是中国现代的著名作家，他曾经写过什么著作，有过什么主要经历，我觉得有必要给学生讲讲，但是老师没有讲，也许是上堂课已经讲过或下堂课要讲。<br />
<br />
但我紧接着就有一个惊喜，这是我过去上学时没有过的，就是老师让学生用4分钟的时间把3300字的文章默读完，我觉得这是对学生速读的训练，是对学生能力的锻炼。她不仅要求学生专心，而且要求学生具有一定的阅读能力。我们常讲人要多读一点书，有些书是要精读的，但有些书是可以快速翻阅的。默读是我听语文课第一次见到的一种教学方法。<br />
<br />
3 研习课<br />
<br />
教室安全未提防震<br />
<br />
第三堂听的是走进研究性学习课。这是我从来没听过的课。听了课我懂了，其实是开阔学生的思维，用我们可以经常接触到的一些事情来深究科学的原理，提出问题，独立思考。这堂课老师讲的是“教室”，就是要建一座好的“教室”应具备哪些条件。学生纷纷回答，几乎我想到的他们都谈到了，从窗户到门，从隔音到节材。<br />
<br />
最后，老师把它概括为四个方面，叫做你想研究什么问题―――研究“教室”；怎么开展研究―――研究“教室”的方方面面；和谁一起研究―――老师和同学；怎样表达研究成果―――把学生的经历、实践和参与结合在一起。<br />
<br />
但我坐在课堂上就在想，非常重要的一点学生们却没想到，就教室而言，建筑安全应是第一位的。学生没想到，教师也没想到。经济适用都想了，但是安全没想到，也就是说学生没有想到防震知识。<br />
<br />
4 地理课<br />
<br />
陕甘宁划入华北错误<br />
<br />
第四堂听的是地理课。我仔细翻了课本。这门课把我们过去的地理与自然地理合并了，甚至扩展到把地理、地质、气象、人文结合起来，是一本综合教材。<br />
<br />
但是讲华北一下子我就听糊涂了，因为课本讲的既不是自然分界，又不是经济分区，也不是行政分区，华北怎么把陕西、甘肃和宁夏包括进去了？课本对中国区域划分的依据不足，无论是自然的、经济的还是历史沿革的划分都没能讲清楚，有的是错误的。<br />
<br />
此外，课本关于中国的区域差异一章就讲了中国的五大区域，即华北、青藏、沿海、港澳和台湾，这就更不全面了。我赞成把地理、地质和气候结合起来，这就如同把人与自然、环境结合起来一样。过去大学的地质地理系就包含这三个方面。我赞成编写教材时把这几方面结合起来，但要把基本概念讲清楚。<br />
<br />
5 音乐课<br />
<br />
唱歌启发学生懂得爱<br />
<br />
最后我听了一堂音乐课，应该说是欣赏了一堂音乐课。老师很活泼，这堂课先是播放了迈克尔・杰克逊的《我们同属一个世界》，这堂课的主题是让世界充满爱。我边听边感到这是一堂艺术熏陶课，也可以说是堂美学课。美学从大的方面讲就是真善美，就是世界事物的真善美，这就是那首歌的真谛。<br />
<br />
因此听完课我就即席讲了一篇话，我说没有爱就没有教育，没有爱就没有一切。一堂音乐课让孩子们通过唱歌来懂得人世间的爱，懂得人世间的真善美。其次是人们的心底。孩子们都有心理活动，就是孩子们心底都有知、情、义。<br />
<br />
这就要求学生要有爱心，懂得爱父母、爱老师、爱家乡、爱祖国。人最起码的爱就是这些，爱父母爱老师爱家乡，再归结起来就是爱祖国了。<br />
