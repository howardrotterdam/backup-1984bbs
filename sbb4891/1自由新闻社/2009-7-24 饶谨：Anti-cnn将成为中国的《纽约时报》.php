													<h2>饶谨：Anti-cnn将成为中国的《纽约时报》</h2>
						
						
																											<div id="postmessage_176682" class="t_msgfont">反CNN网站饶谨：以小搏大 在新闻交锋中成长<br />
2009年07月22日14:56&nbsp;&nbsp;来源：人民网-传媒频道<br />
<br />
<img src="http://media.people.com.cn/mediafile/200907/23/F200907231436306502201424.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
7月20日，反CNN网站创始人饶谨做客人民网传媒沙龙。人民网记者 燕帅 摄<br />
<br />
<a href="http://media.people.com.cn/GB/40606/9700902.html" target="_blank">http://media.people.com.cn/GB/40606/9700902.html</a><br />
<br />
　饶瑾说，“7・5”事件发生之后，我们对西方媒体的报道极为关注。因为越是出大事的时候，西方媒体对中国的歪曲报道和一些莫名其妙的报道就会比较多。比如有些对事情的真相加以掩盖，例如，在整个事件中，把大多数的镜头聚焦在中国政府如何残酷镇压，都是军警的照片，对受伤的普通民众受伤的镜头很少。<br />
<br />
　　西方媒体的手段有很多，比如歧视性民族政策，或者“民族冲突”这样的字眼，我们很多网友认为应该定性为恐怖袭击的事件，他们把这种事件引向民族间的冲突。但是我们知道，这其实很明显是双重标准。两年前，在巴黎郊区也发生过少数分裂主义的骚乱，1992年旧金山也发生过黑人的大暴乱，当时为什么媒体就不把这个报道成种族冲突或者民族人权之类的问题，就是定性为犯罪分子与暴乱，中国也没有对他们指手划脚。<br />
<br />
　　关键是我们内部的团结。从去年“3・14”事件到现在经历了一年多的时间了，经历了各种风风雨雨。我们刚刚建立的时候，遇到黑客攻击，当然我自己也收到过一些恐吓信之类的。可能有些人从心理上给我压力，让我不要再做这样的事情。　但是我觉得这种恐吓和威胁对我是不起作用的。<br />
<br />
饶瑾评价今年国内媒体对新疆事件的报道时说“比去年强很多”。比如第一时间公开信息，第一时间进行针锋相对的反驳，甚至揭露一些西方媒体的歪曲报道。我们的媒体反击以前没有那么强烈，这次虽然谈不上主动，但是至少没有被人太牵着鼻子走。我认为我们国内的媒体也在逐步地成熟。<br />
<br />
　　我们的英文媒体很少在西方引起什么反响，比如央视、新华社力求打造国际媒体，但是目前来看，在欧洲主流社会能够引起的反响还是非常微弱的。因为毕竟现在是英语为主导的世界，英语还是西方的主流语境，我们要想在这种语境下取得很大的突破还是相当困难的。我们现在能找到的更多的是一些画面、用词或者明显的造谣，但是如果要想真正地从根源上扭转这种外宣上特别被动的局面，一定需要我们自己有一种随时随地发出自己声音的措施，比如，AC能够从网络媒体做起，达到像《纽约时报》的程度，我认为这完全有可能，如果大家愿意把时间和精力放在这上面，愿意做这个事情，还是能够做实现的。<br />
 <br />
<br />
　　作为一个民间草根的力量去反驳一些西方媒体，而且是路透社、《纽约时报》这样的西方主流大媒体，饶瑾坦言，我们以小搏大，虽然力量很小，但是击中的都是让他们无话可说的事实，所以可以理直气壮地和他们进行正面交锋。我们从诞生之日起，就是在和新闻交锋中成长起来的。所以我们没有什么好怕的，大风大浪都经历过了。<br />
<br />
　　面对部分西方媒体歪曲报道的时候，我们不应该只是讨论和气愤，而应该有所作为。饶瑾对此表示“非常认同”，他说，首先，我们确实让对方无话可说，在铁证如山的情况下如果出了媒体报道的事故，我们可以先向他们的公司提出抗议，由他们的公司对记者进行处罚，如果他们不处理的话，将损害自己的声誉，媒体当事人也会因此更加收敛。<br />
<br />
　　另外就是直接起诉媒体本身。现在的新闻媒体也有竞争对手，我们可以向它的竞争对手举报。所以，首先还是以事实为基础。另外，要有效的、有质量的，一定要非常专业的。比如尽量采用英语或者西方主流社会能够接受的表达方式，这是最有效的。<br />
<br />
提到西方记者是不是应该回炉补补课的问题，饶瑾认为，他们的专业性、敬业精神是我们的记者达不到的。说实话，西方媒体记者敬业精神是很强的，为了达到他们的目的，事前可以把稿子写好。比如，一些西方媒体明面上标榜自己是客观、公正，不受任何控制，但是它毕竟代表着一些政府或者财团的势力，所以它不可能做到百分之百的公正和客观。关键是我们遇到这种情况的时候，有没有力量进行反击，或者能够在一些事情发生以后，我们的消息能比它更准确。我觉得这也是中国新闻界、中国的媒体人需要追求的一种状态。<br />
<br />
　　尤其是西方媒体对其老百姓的误导。很多人以前对中国没有这种敌视，没有这种概念。但是经过他们误导以后，比如土耳其媒体，完全是颠倒黑白，包括他们的总理也发表了很多对中国非常不友好的言论。这样势必引起他们民间对中国不友好的情绪。在很多情况下，丑化中国、攻击中国，才能达到他们意图分裂中国的目的。<br />
<br />
饶瑾说，我们要解放思想，在事情发生以后，我们能够比西方记者更加及时、更加有效地把真相报道出来，谣言自然会更加少一些。这也是我们国内的媒体同行应该向西方记者学习的，有事情总是第一时间就能赶到现场，总是第一时间发出声音，这也是专业性的一种表现。<br />
<br />
　　西方媒体为什么不能客观报道新疆的“7・5”事件？饶瑾对此说，只要有国家与国家力量的博弈，有各方力量的角逐，有国际关系风云变幻的存在，就一定会有不同的政治利益集团或者经济利益集团在操控着不同的媒体，让他们发出所谓公正自由的声音，达到他们的目的。<br />
<br />
饶瑾说，首先AC是我自己想做的，而且是非常有意义的事情，不是人云亦云，是经过独立思考的， AC可以成为全球华人的共同事业。虽然目前的状况，我还不是特别满意，毕竟我们面临着很多经验的欠缺。在面临大事的时候，我觉得更能显示出AC人的团结。每当重大事情发生的时候，比如类似“7・5”事件，我们网站的流量又上升了好几倍。所以我觉得，并不是大家不关注这个事情，而是一旦有一些针对中国不好的行为之后，所有的人还是不约而同地想起AC这个平台，包括西方媒体也会非常关注。<br />
<br />
　　如果真正地从气势上，从根本上彻底地把西方媒体这种嚣张气焰打下去，一定需要我们自己很专业的编辑队伍，需要专业的撰稿人队伍。从反CNN网站的诞生到现在也经历了很多风雨，确实也面临过以小搏大的情况，以非常弱的力量撼动了整个西方媒体所谓的“公正、自由”的面纱。但是如果从根基上，真正地发出中国人的声音，我觉得还是需要有一个自己正面的形象。所以，有破有立，我们的破是打破他们的话语垄断，这个立，就是我们如何发出自己的声音，就是建立自己的国际媒体。<br />
<br />
