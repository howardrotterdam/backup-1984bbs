广西数千人攻铝厂杀3员工<br />
<br />
【明报专讯】广西壮族自治区百色市靖西县一间铝厂与当地村民爆发冲突，数千村民以石头、土制炸弹攻击铝厂，十多辆车辆被砸烂，有消息称3名铝厂员工被打死，十多人受伤，百色、南宁两地的防暴警察和武警赶到当地戒备。<br />
<br />
排污爆冲突重兵戒备<br />
<br />
互联网消息称，事发于7月11日，位于靖西县的广西信发铝厂在附近村庄修建公路，由于村民指矿场长期污染水源，上前理论，双方发生推撞，信发召共300员工乘车到村中「增援」，持水管和木棍打伤数名村民，村民以石头还击，并把车辆砸坏。<br />
<br />
