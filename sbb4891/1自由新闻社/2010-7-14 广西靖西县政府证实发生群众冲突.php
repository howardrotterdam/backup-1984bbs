6```因为信发铝厂，靖西的河流已被严重污染，河水呈红色并伴有粘糊状。要知道靖西河流是流出境外，进入越南。几个月前越南民众已因河水遭污染向中国提出抗议，但不知道靖西人民政府是怎样维护国威维护国家利益 <br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
2010年7月11号 那天......<br />
信发在凌晚村附近开公路，<br />
因为村民没有收到原来答应的款数 <br />
村里人不同意就把前去讲道理的村民打了<br />
并把人推进河里<br />
还打电话叫总厂的信发员工去帮忙打村民<br />
还用钱请来在靖西的混混<br />
2010年7月11号下午<br />
信发员工和靖西的混混约300名左右开了几辆车前往凌晚村打村民，凌晚村是在靖西的第二大村。在进入村子的时候将在村里种地的几个村民打伤，却遭到全村的村民反击，遭到上千名的村民在山上仍石头，砸坏了好多辆载人来打架的信发车，此前信发人开来一 辆载满水管和木棍的车辆，把水管和木棍堆在路边还吩咐前来打架的信发员工和靖西的混混，每人拿了水管后进村看到村民就打，谁也没有想到没进到村里就被村民打退了，还有见义勇为的邻村也前来帮忙，一起打退了前来打村民的信发员工和靖西的混混。<br />
12号早上靖西靖宇车站都挤满了人，两百多人凌晚村在外务工的村民，已经顺利的赶回了家乡，他们为了守住村庄，不惜一切代价连夜包车回家，因为7月11号被信发员工放言等到天黑了，开推土机把凌晚村子移为平地。 <br />
<br />
<br />
<br />
<br />
<br />
<br />
至此 ，711 靖西暴动 形成<br />
<br />
<br />
在次之前有一年的某一天也出现这类似的情况，在高美村的村民不让信发铝矿厂采矿，也被信发老板叫保安进村打人，后来靖西公安也没有对此事做出反映<br />
<br />
<br />
2010年7月12号&nbsp;&nbsp;事态又进一步加剧<br />
昨晚记者看到很多凌晚村民并没有睡觉，在山上守了一个晚上。<br />
数万群众集结彭羚信发洗矿场，与守卫发生冲突后局势难以控制，大批群众冲进场内打人、砸设备和车辆......而信发这边的处理也令人发指，数十辆运矿车载几百个山东人，十几辆铲车打头阵，也冲进了洗矿场方向，扬言要铲平所有村寨，杀光所有闹事村民。一路见人就打，最令人无法接受的是就连七旬老人和幼小儿童都不放过，有一七旬老人放牛回来，见来势汹汹的人群就急忙避开，没想到被某位信发身手敏捷的领导抓了回来，一阵拳打脚踢之后已经奄奄一息，再被扔进河里淹死。惨不忍视。后面就连幼小儿童也难逃厄运，均受到了同样的待遇。<br />
2010年7月13日，凌晨，数万愤怒的群众集结到了信发生产厂区，扬言要铲平信发厂报仇雪恨。信发的人出动所有的员工和铲车抵御，不懂是谁下的命令“过来的人全部杀死，车辆不放过”许多带队的主任按部就班，防御变成了主动出击，误砸了十几辆车，还好没人伤亡......这也为后面的更大冲突点燃了导火索。有一辆面包车路过，结果被信发的山东人截下砸烂了，后面发现车上就一家三口人，孩子和女人正在害怕的失声哭泣......之后不到两个钟头，引来了另外一群一百多人的愤怒的群众冲击门岗，还好有一队防爆警察过来镇压，稳住人心，不然冲破关卡那是可能的。石头、钢弹、汽油瓶、土炸药满天飞，幸好是没有太大人员伤亡，冲击了两次之后就退去......<br />
冲突持续了将近十个钟头，目前冲突已造成数百人受伤，数十人死亡。 <br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
7月13号&nbsp;&nbsp;近千名居民&quot;净化河流 美化靖西&quot;的口号游行 <br />
<br />
<br />
<br />
<br />
<br />
<br />
南宁及百市各县的防暴及武警部队已陆续赶到控制局势。十几部桂Ａ（南宁市）警车开到靖西，其中包括两部装甲车。 <br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
在此，向想拯救家园却无能为力的大部分政府人员致敬，因为上压下，你们也是无奈。向驻靖西边防部队团长敬礼，因为您的一句：“让我去抗洪救灾可以，让我去打压群众，我是不会去的。”于是有关领导只能向周边县请求警力援助，并向百色南宁警力求助，没办法，武警是地方部队，只能前来。但靖西人民从不怪罪武警，因为武警仅仅是执行命令而已。<br />
就因为一个大厂投资300亿，就可以为所欲为了。环保部门应该没有审批吧？还有一些领导的贪污 让那信发铝厂免税5年， 5年！5年 10年 20年 一辈子 一亩地一次性给9千元 ，农民吃什么呢？ 只是想还我们一个美好的家园，本来好好的旅游大县。现在已经被污染，农民颗粒无收 。如果在这样下去，龙谭很快也被污染，全县城的居民饮水问题谁来解决？<br />
<br />
广西百色市靖西县的美 <br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
污染是个无底洞,我们只想好好保卫我们的家园。可有些人为了眼前的利益，出卖了子孙后代</div>

							
							
							
							
