来源： 段荣鑫的日志<br />
不知道这算不算政治贴 如果是的话请删除&nbsp;&nbsp;我只是想让更多人知道真相<br />
好多人都说看不到图 再发一个图床 两个图床<br />
<br />
<br />
上百度查一下 靖西事件 靖西铝厂事件 靖西暴动 很多都被禁了 要不就被和谐了 <br />
在此之前看到媒体上的相关报道都是不真实的，什么靖西群众聚众违法，只是腐败政府的一面之词，是为了逃避责任和推脱罪行我记得上初中的时候政治课 言论自由&nbsp;&nbsp;难道政府隐藏实情也不犯法吗？ <br />
<br />
<br />
<br />
<br />
<br />
<br />
关于7.11起因 ：<br />
1```信发铝厂征求土地一亩赔偿3万元人民币，经过政府部门的手后，到农民手中只有每亩9千元 。当时已有群众向上级反映，但后来却是不了了之。当时农民的愤恨之心已起。 这使我想起了中国古代的一词“官官相应”！<br />
<br />
2```后来铝厂的人疯狂的开采当地的铝土矿，却没有进行开采后的土地回放，导致靖西山河破碎，生态遭到严重破坏。不知道靖西县人民政府的人平时吃的是米饭还是米糠，但他们的身影却经常出现在各种酒店里。 <br />
<br />
3```再后来发生塌方，信发铝厂将百吨水泥倒入地下河，地下河河道被堵，凌晚村水位猛涨，成一片汪洋，从此靖西又多了一个“水库”！凌晚村村民只能在“水库”旁搭起帐篷，作为临时居住所。但靖西县人民政府却隐瞒不上报，还向村民解释说是发生地震才导致“水库”诞生！多么美丽的解释，但地球人都不会相信！凌晚村村民直到现在还住在帐篷里。这时温总理的话“为人民排忧解难”又在我的耳边响起！ <br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
4```信发铝厂的保安大多是山东籍的退伍军人，平时极为嚣张，多次殴打当地村民。靖西民警也无可奈何。<br />
<br />
5```近期铝厂的污染越来越严重，铝厂附近的庄稼因受污染全部枯死 <br />
<br />
现在的河流 <br />
<br />
<br />
以前的河流 <br />
