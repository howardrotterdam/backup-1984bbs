																											<div id="postmessage_292960" class="t_msgfont">中国已免除非洲32个国家150笔到期对华债务<br />
2009-10-21 来源: 中国新闻网(北京)　<br />
核心提示：截止2009年第一季度，中国已顺利免除非洲32个国家的150笔到期对华债务。中国常驻联合国副代表表示，中国将继续向非洲国家提供了包括无偿援助、无息贷款、优惠贷款在内的各类援助。<br />
<br />
中新社10月20日电&nbsp;&nbsp;中国常驻联合国副代表刘振民二十日表示，截止二00九年第一季度，中国已顺利免除非洲三十二个国家的一百五十笔到期对华债务。 <br />
<br />
刘振民大使是当日在第六十四届联大作关于非洲议题发言时作上述表示的。他说，金融危机爆发以来，中国克服自身困难，继续向非洲国家提供了包括无偿援助、无息贷款、优惠贷款在内的各类援助。中方援助重点用于加强中非在农业发展、基础设施建设、人力资源培训和医疗卫生等领域的合作，这些都是“非洲发展新伙伴计划”的优先领域。 <br />
<br />
刘振民说，在二00六年中非合作论坛北京峰会上，中国政府宣布将免除与中国有外交关系的三十三个非洲重债穷国及最不发达国家截止二00五年底对华到期政府无息贷款债务。截止二00九年第一季度，中国已顺利免除其中三十二个国家的一百五十笔到期对华债务。 <br />
<br />
<br />
刘振民说，此外，中国政府高度重视非洲国家在市场准入、贸易不平衡等方面的关切，采取了大量措施加强与非洲国家的贸易关系。二00八年中非贸易总额为一千零六十百点四亿美元，同比增长百分之四十五点一。其中从非洲进口五百六十亿美元，同比增长百分之五十四。 <br />
<br />
他表示，今后中国将在农业、教育、卫生、医疗、清洁能源等领域向非洲国家提供进一步援助和支持。我们也将继续积极支持非洲国家预防冲突、解决冲突和建设和平方面的努力。 <br />
<br />
刘振民还表示，为实现“非洲发展新伙伴计划”等目标，国际社会应高度重视以下几点：应尽快落实援助承诺；应提供更多额外的融资；应尊重非洲国家的自主权；应扩展南南合作；应帮助非洲国家进行能力建设；应加强国际机构的作用。 (本文来源：中国新闻网 作者：孙宇挺)</div>

							
							
							
							
