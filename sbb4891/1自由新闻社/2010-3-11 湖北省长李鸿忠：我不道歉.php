																											<div id="postmessage_512901" class="t_msgfont">干部是人民公仆，媒体替人民监督<br />
本文来源于《财经网》　 2010年03月11日 17:58&nbsp;&nbsp;<br />
两会代表委员中个别领导干部依仗公权力的“雷”人，则是不能被允许的。回避实质问题的“灭火”，有可能把小事闹大、大事拖炸，对于维护地方利益和政府形象甚至是十分有害的 <br />
<br />
<br />
　　钟沛璋<br />
<br />
　　我86岁了，在医院养病期间，以一个老党员、老记者的天性，关注着全国两会的进展和各地新闻。<br />
<br />
　　2月27日，温家宝总理再次“触网”，与全国网友聊天，一聊就是两个多小时，从物价、房价、大学生就业、民工荒、三鹿奶粉，谈到医疗改革、教育去行政化、“三公消费”，还谈到自己能体会“蜗居”的苦，最后再与网民“勾手相约”来年再见。<br />
<br />
　　早在2008年6月20日，胡锦涛总书记就曾来到人民网，登陆“强国论坛”，与网友交流。“知屋漏者在宇下，知政失者在草野。”胡温率先垂范，鼓励网络问政，这是一个大国领导人的政治风范。<br />
<br />
　　两会期间，人民网、新华网和一些门户网站纷纷开设“E两会”、“网民议事堂”、“我有问题问总理”等栏目，网络议政成为人大代表、政协委员议政的一个重要意见来源和民意基础，给两会政治带来了一股清新的空气，令人振奋。<br />
<br />
　　我注意到，两会代表、委员们的某些提案、议案和对媒体的表态，由于脱离主流民意，被网民冠以“雷人”二字，受到公众批评，需要代表们深自警思。代表委员们参政议政水平有个逐步提高的过程，这是可以理解的。但两会代表委员中个别领导干部依仗公权力的“雷”人，则是不能被允许的。<br />
<br />
　　我想谈谈对全国人大代表、湖北省省长李鸿忠训斥《人民日报》旗下《京华时报》女记者，并夺走录音笔一事的看法。记者关心邓玉娇案，是新闻媒体的职业素质要求。党的十七大要求切实保障人民群众的知情权、表达权、参与权和监督权。<br />
<br />
　　甚至早在1948年，刘少奇同志与华北记者团讲话中，就郑重要求党的新闻工作者，帮助人民和中央通气，考察党和政府的政策到底对不对，错了就提出来，时刻准备修正和补充。这是共产党的新闻政治理念。<br />
<br />
　　记者在邓玉娇案发生后的第一次全国“两会”上，向湖北行政长官提问这一曾经震动全国的社会热点事件，符合这样的新闻政治理念，也是新闻工作者的职责所在。<br />
<br />
　　一个负责任的地方领导干部，有义务借国家议会的场合，梳理和反思处置邓玉娇案的经验教训，并对于避免类似案件再次发生、疏导民意、化解民怨提出建设性的思考。如果确实发生因此而指责记者的现象，那是落后于共产党在1948年的政治觉悟的。<br />
<br />
　　我了解到，近年来一些地方领导人经常抱怨：新闻媒体特别是互联网把地方搞乱了，动不动就想封网，抓捕敢向政府提出批评意见的网民。邓玉娇案中，基层政府甚至以“防雷击”为名，停止了电视信号转播。我看这些同志对胡锦涛总书记提出的“舆论引导新格局”的战略判断，缺少认真的学习体会。<br />
<br />
　　他们至今还搞不清楚，新闻的发生是第一位的，新闻的报道是第二位的。闹出矛盾来，解决问题是根本，只想着舆论“灭火”是治标不治本。回避实质问题的“灭火”，有可能把小事闹大、大事拖炸，对于维护地方利益和政府形象甚至是十分有害的。<br />
<br />
　　我们提出媒体和记者要坚持正确的舆论导向，目的是配合党和政府解决现实生活中影响科学发展、社会和谐的实际问题，而不是一味要求他们回避现实矛盾，以鸵鸟姿态，放任基层政府的不作为和乱作为，坐失通过舆论监督维护社会稳定的良机。<br />
<br />
　　3月11日，我看到报章上李鸿忠同志就此事的公开回应，感到很悲哀。堂堂地方大员对于在国家议会这样的庄严场合，夺下记者录音笔这样的明显失态行为，竟然没有一丝歉意，辩解说“我们就担心她不是记者，就把录音笔拿去看看了”。首都两会驻地不是邓玉娇的家乡野三关，那里去年曾经发生过动用公权力抢夺记者采访器材、殴打记者并逼着记者认错的事件。<br />
<br />
　　记者问：“许多网民要求您向记者道歉，您怎么想？”李鸿忠同志断然回答“我觉得不涉及道歉”，接着以长者的口吻宽容这位受辱女记者的莽撞：“很多年轻人刚从业，也很不容易”，“有的时候，有一些误解啊，像传递信息不很全面啊，我看也不用（道歉），慢慢就好了”。后面这句话，意思完全弄反了，似乎是记者本该向他道歉，只是因为领导同志宽容大度，才不跟年轻人计较了。我看这样的表态不仅极为勉强，也缺乏共产党人应有的诚恳和诚实。<br />
<br />
　　温家宝总理刚刚在2月27日与网友交谈中表示：我是“公共财产”，属于人民。公权力形象和公信力属于“公共财产”，需要自觉地、无条件地接受公众，特别是新闻舆论的监督。对抗监督，就得鞠躬道歉，这是立党为公、执政为民的原则问题，没有什么“慢慢就好了”的可能。此事糊里糊涂地过去，只会拖累党的形象、政府的声誉！<br />
<br />
　　这场不该发生的争端，使我想起整整30年前青年厨师陈爱武批评商业部长事件。北京丰泽园饭庄的厨师陈爱武，向《中国青年报》反映，商业部部长王磊搞特权，吃一顿客饭交的钱还不够买一碗汤。<br />
<br />
　　我当时任《中国青年报》副总编辑，安排记者去调查，发现确有其事。1980年10月16日，在第一版头条位置刊登通讯：《“敢于向特权挑战的人――记北京丰泽园青年厨师陈爱武》。我为这篇通讯配发社论《改革者，鼓起你的勇气》，强调：“不管职务多高的干部，他们都是人民的公仆，而决不是人民的老爷。他们都必须接受人民的监督。”当天早上，中央人民广播电台“新闻和报纸摘要”节目摘播了通讯和社论。<br />
<br />
　　见报当天，王磊部长就向中纪委作了书面检查，除了认错并表示愿意如数补足少付钱款外，还请求给予党纪处分。王磊部长又把这一检查同时送给国务院领导同志，并请商业部党组将他的检查印交各局，“向全体同志宣读”。当天下午，商业部党组举行会议，“学习和讨论”了《中国青年报》发表的报道和社论。<br />
<br />
　　王磊还分别给北京市第一服务局和丰泽园饭庄写信，表示：“听广播之后颇受教育，也颇惭愧，对陈爱武同志这一行动，表示衷心感谢和钦佩……请你们协助我查清，我在各饭店吃过饭少付的价款，通知我，我也补足”。<br />
<br />
　　80年代初，尽管也有“不正之风”的困扰，但党风和社会风气总体上比较清纯。尽管王磊部长是1936年参加革命、1937年入党的老干部，但被批评后立刻对中央、对媒体、对提出批评者、对公众诚恳认错道歉，痛痛快快，不拖泥带水，没有反过来质问记者“你怎么老纠缠这个事情”，更不会威胁说“我要给你们社长讲”。<br />
<br />
　　1982年3月，王磊被免去商业部长职务。1994年王磊同志辞世。在舆论监督面前，他知错能改、胸襟坦白，令我感动。今天想来，恍若隔世。<br />
<br />
　　当时也曾有领导同志认为，陈爱武批评商业部长不好，会煽动群众对政府不满等等，这实际上就是为王磊同志开脱。他们还定了两条杠杠，一是点名要经过批准，一是要先党内后党外。<br />
<br />
　　我当时就针对这两条杠杠说：“我们不禁要问你们搞特权是谁批准的？你们搞特权的时候有没有想到党？有没有想到党外的影响？为什么现在要抬出党来为特权保驾了？……现在的实际情况不是对干部保护少了，而是人民监督渠道少了，人民监督而不被打击报复的保护少了。报纸是人民实行民主进行监督的一条很重要的渠道，你做对了就表扬，做错了就提醒你，这有什么不好呢？这样人民的民主生活就正常了，党的民主生活也正常了。”我在人民日报就此事撰写文章《开一代民主新风》，提出：“利用报纸来监督公仆……是建立正常民主生活不可缺少的渠道。”<br />
<br />
　　中国人民为建立一个民主共和国，前仆后继地奋斗，从1949年算起经过了60年，从1911年辛亥革命算起即将迎来第100年。几代人的流血流汗，为的是什么？就是为了人民成为国家的主人。用今年“两会”政府工作报告中的话来说：“我们所做的一切都是要让人民生活得更加幸福、更有尊严，让社会更加公正、更加和谐。”这是政府工作报告中最令人动容的一段话。<br />
<br />
　　我注意到一个细节：温家宝总理做完政府工作报告，胡锦涛总书记与他热情握手。这是我们党面对全中国人民的庄严宣示。国家的存在是为了人民生活的幸福和尊严，不是要把人民管住，无处说话。<br />
<br />
　　美国前总统小布什就任时说过一句话：“人类最伟大的成就就是实现了对统治者的驯服，把官员关到笼子里。我现在就是站在笼子里给你们讲话！”美国总统也要受到美国人民和国会议员的监督。在我们社会主义中国，领导干部不能够无法无天，应该受人民监督。<br />
<br />
　　当前中国处在一个转折时期，经济飞快发展，有可能成为世界第二大经济体，但同时腐败、贫富差距、社会不公正……矛盾很激烈。当地方官不容易，要落实好中央的正确决策，驾驭复杂局面，处理好各种矛盾，提高执政能力和执政艺术，但有一条，地方官首要先做人民的公仆，而不是人民的老爷。<br />
<br />
　　我很赞同中宣部部长刘云山同志近日在第一期全国县委宣传部长培训班的讲话：“要把舆论引导的任务落实到基层，善待媒体、善用媒体、善管媒体，支持媒体采访报道，正确对待舆论监督，提高同新闻媒体打交道的能力。”<br />
<br />
　　当政者常说要有忧患意识，最大的忧患莫过于人民的知情权、表达权得不到充分保障，官员缺乏有力的舆论监督。我们仍要继续高举“五四”运动，“民主与科学”的大旗。现代国家的官员们都受新闻舆论监督。尊重舆论监督的官员，才有道德的力量。没有道德力量，与民心、党心相背，任何官员手中的权力都是靠不住的。<br />
<br />
　　近日另一条消息让我眼前一亮。在全国“两会”的民主气氛中，湖北十堰市公安局出手纠正了郧西县以诽谤罪抓捕网民的错案，对网民“零点三六”（陈永刚）给予国家赔偿。<br />
<br />
　　在当地论坛上，县委、县政府发布《郑重声明》称，“向陈永刚本人和社会公开道歉，真诚欢迎社会各界和广大网民批评监督郧西工作”。声明之后，县委书记叶战平亲自跟帖：“我个人对十堰市公安局关于郧西县公安局对陈永刚一案的复核处理意见无异议。同时，希望县公安局总结经验教训，更好地依法履行职责，维护一方平安。”<br />
<br />
　　这样的湖北干部，值得大声喝彩。<br />
<br />
　　（作者为中宣部新闻局前局长）<br />
