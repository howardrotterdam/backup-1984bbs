事实上，《京华时报》是人民日报社下属的一份市场化的都市报。在中国，由于党报的优越政治地位，作为市场化媒体的子报由于担心采访被拒绝，经常会报上母报的名义进行采访。这不规范，却是相当普遍的现象。<br />
<br />
《新京报》的一名编辑说，很多记者，当遇到李鸿忠周继红等大人物的反诘时，往往都报母报的名，比如《京华时报》的会说自己是《人民日报》的，《新京报》的会说自己是《光明日报》的。我明白这有出于自保的无奈，不过多少也有点自甘下贱，考虑到母报与子报的关系，准确地说这该叫“自甘上贱”。<br />
<br />
但根据李鸿忠的暗示，这名女记者身份可疑，因此，抢夺她的录音笔是有理由的。李鸿忠的这一说法并不是第一次。<br />
<br />
事件发生的第二天，李鸿忠抵达人民大会堂参加人大会议，一通过安检机，即被多名香港记者包围追问。李一脸不悦，本想调头离开，却被多部摄影机阻挡。众记者穷追不舍，被问及会否向女记者道歉时，李只吐出四个字“她搞欺骗”，接用手拨开记者的麦，快步走入会堂。<br />
<br />
李鸿忠的故事编得并不圆满，当时在场的记者很多，虽然现在还没发现有现场的视频流传，但李省长遮不住这么多记者的眼睛，虽然抢走了一只录音笔，但却没能抢走现场那么多的录音笔，互联网上也流传着当时现场的录音。<br />
<br />
综合在场的许多记者的描述和现在网上可以找到的现场录音，可以有把握地说：李鸿忠在说谎，他的幕僚们设计的这个情节穿帮了。<br />
<br />
在场记者回忆说，一开始，《京华时报》女记者刘杰抢着提最后一个问（现场数十记者的录音笔、摄像机、相机此时还围着李鸿忠）“请问省长你怎么看待邓玉娇？”，省长大人马上阴沉下脸，一言不发，现场气氛降到零点。<br />
<br />
此时，李鸿忠伸出左手（现在江湖人称闪电手）抢下刘杰的录音笔。他动作隐蔽，而且刘杰个子矮小，录音笔位置偏低，所以当时很多人没看见李鸿忠的动作。<br />
<br />
随后，李鸿忠径直往贵宾厅大门走去，快到门口时，他又回转身体，有了后来众所周知的“怒斥女记者”一段（见本台此前报道），而到了此时，李鸿忠才知刘杰的“人民日报”身份。<br />
<br />
请注意顺序！<br />
<br />
也就是说，提问DD抢录音笔DD怒斥，而不是对《新快报》撒谎的逻辑：提问DD回答DD看录音笔。在现场的一名记者说，“还原了这个逻辑顺序，李鸿忠怎么都无法撒谎欺骗媒体了。”<br />
<br />
在《新快报》的采访中，李鸿忠省长反而唱起高调，怪这个女记者不够大方，他说，“我觉得她应该更开朗一些，大大方方的。”<br />
<br />
事实上，被这么一位中央委员，省部级高官当场怒斥后，这名女记者当场红了眼眶，后来再也不敢接受境外媒体采访。<br />
<br />
而在李鸿忠的这次采访里，他摆出高姿态，自称善待媒体，反过来展现“高姿态”。他说，“媒体也很辛苦，特别是很多年轻人刚从业，也很不容易。有的时候，有一些误解啊，像传递信息不很全面啊，我看也不用（道歉），慢慢就好了。”<br />
<br />
“似乎是记者本该向他道歉，只是因为领导同志宽容大度，才不跟年轻人计较了。”钟沛璋评论说。<br />
<br />
86 岁的老干部，原中宣部新闻局局长钟沛璋在一篇长文《干部是人民公仆，媒体替人民监督》。中回忆了1980年10月16日，《中国青年报》刊发报道，采访北京丰泽园青年厨师陈爱武，揭露当时的商业部部长王磊吃饭少付钱，吃一顿客饭交的钱还不够买一碗汤。<br />
<br />
钟沛璋说，王磊部长是1936年参加革命、1937年入党的老干部，但被批评后立刻对中央、对媒体、对提出批评者、对公众诚恳认错道歉，痛痛快快，不拖泥带水，没有反过来质问记者“你怎么老纠缠这个事情”，更不会威胁说“我要给你们社长讲”。<br />
<br />
钟沛璋说，看到报章上李鸿忠就此事的公开回应，感到很悲哀。“堂堂地方大员对于在国家议会这样的庄严场合，夺下记者录音笔这样的明显失态行为，竟然没有一丝歉意。”<br />
<br />
他说，“首都两会驻地不是邓玉娇的家乡野三关，那里去年曾经发生过动用公权力抢夺记者采访器材、殴打记者并逼着记者认错的事件。”<br />
<br />
《人民日报》原副总编辑周瑞金则说，3月5日上午9时，温家宝总理在政府工作报告中宣示：“创造条件让人民批评政府、监督政府”。<br />
<br />
“李鸿忠此举，不啻于给了温总理一个耳光”，什么,“创造条件让人民批评政府、监督政府”，不是一句空话吗？<br />
<br />
这两篇评论已经被国新办下令《财经网》删除，昨天，杭州的《青年时报》编辑则致电病床上的钟老先生，要求在报纸上转载，并向老人表达敬意。<br />
<br />
老人回答：“谢谢关心，身体尚可，可以转载。”</div>

							
							
							
							
