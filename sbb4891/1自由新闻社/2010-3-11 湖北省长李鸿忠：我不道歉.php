																											<div id="postmessage_511702" class="t_msgfont">湖北省长李鸿忠：录音笔事件是误会不涉及道歉<br />
2010年03月11日03:15荆楚网我要评论(6) 字号：T|T <br />
湖北省长李鸿忠：录音笔事件是误会<br />
<a href="http://news.qq.com/a/20100311/000183.htm" target="_blank">http://news.qq.com/a/20100311/000183.htm</a><br />
“我们担心她不是记者，就把录音笔拿去看看了。”昨日上午，记者在湖北团全体会议上见到了因拿走女记者录音笔而受到公众广泛关注的湖北省省长李鸿忠，李省长向记者详细地解释了事情的来龙去脉。<br />
<br />
前日，网络上疯狂流传的湖北省李鸿忠省长拿走女记者录音笔一事，让无数的记者聚集在大会堂门口，为的就是能够堵住这位两会上的新闻人物。不过，当天无一人能寻到李鸿忠的影踪。<br />
<br />
昨日上午，记者又赶到湖北团，终于找到了李鸿忠省长，单刀直入采访了“录音笔”事件。在将近10分钟的采访过程中，李省长语调温和，对记者的每个问题都耐心倾听。他一再强调自己对媒体的态度是开放的。<br />
<br />
“比如那天小撒（撒贝宁）采访我的时候，好几个其他报社的记者让我转个身照相，我就把身体转过来，让他们照个够。我们就是要创造条件给记者采访。”李省长举例说。<br />
<br />
当记者问到是否需要向女记者道歉时，旁边有一位记者打断：“这个问题怎么能问呢？”但李鸿忠并未受影响，而是继续回答提问。<br />
<br />
谈起因<br />
<br />
她支吾说是《人民日报》的<br />
<br />
记者：网上盛传你与京华时报记者发生了冲突，您能谈一下吗？<br />
<br />
李鸿忠：我请教她“您是哪个新闻单位的”，她说她是《人民日报》的。我就又问她：“你是《人民日报》的吗？”她就支支吾吾没再答了。后来我们得知她不是《人民日报》的，是《京华时报》的。不过《京华时报》也没关系，我觉得每个媒体都有采访啊、新闻监督的权利，这都很正常。我觉得就理直气壮地说《京华时报》就好了，没什么问题。<br />
<br />
谈拿录音笔<br />
<br />
担心不是记者就拿了看看<br />
<br />
记者：舆论好像集中在女记者的录音笔上。<br />
<br />
李鸿忠：这个是因为她没有痛快讲（她的单位），可能她觉得自己是《京华时报》的却说是《人民日报》的，不太好吧。我们就担心她是不是其他单位的，不是记者，就把录音笔拿去看看了，看看是什么内容。<br />
<br />
谈对媒体态度<br />
<br />
新闻舆论的监督是必须的<br />
<br />
记者：您觉得政府和人大代表应该怎样面对媒体？<br />
<br />
李鸿忠：我2004年的时候刚到深圳（时任深圳市市长，编者注）时就说过，新闻媒体有“三光”作用。第一，就是要阳光。总理讲，权力要在阳光之下运作，什么东西亮在阳光之下，就一清二楚。第二个是目光，群众的目光，人民的目光，这也是监督的一部分。第三个，是激光。用激光做手术不出血，容易治病，创伤面又小，舆论监督是改正问题、纠正错误的好办法。我当时概括的“三光”作用就是要高度重视媒体。<br />
<br />
我认为新闻舆论的监督和人大法律监督、人民政协监督、人民群众的监督一样，都是必须的。<br />
<br />
谈道歉<br />
<br />
我看也不用，慢慢就好了<br />
<br />
记者：网络上一边倒地觉得记者受了委屈？<br />
<br />
李鸿忠：我觉得她应该更开朗一些，大大方方的。<br />
<br />
记者：您觉得她也有错？<br />
<br />
李鸿忠：我也不认为什么有错，在这个过程中，就要互相理解。<br />
<br />
记者：许多网民要求您向记者道歉，您怎么想？<br />
<br />
李鸿忠：唔……我觉得也不要太讲这个。其实认识一个人也不是光是某一个方面的。有一些误解，不要紧。<br />
<br />
记者：您觉得不用道歉？<br />
<br />
李鸿忠：（略微激动）我觉得不涉及道歉。大家的目标都是为了一个――社会的和谐发展。党政工作是为了社会和谐发展，媒体也很辛苦，为社会的公平正义鼓与呼，鞭挞社会的不良现象。有的时候，有一些误解啊，像传递信息不很全面啊，我看也不用（道歉），慢慢就好了。<br />
<br />
特别是很多年轻人刚从业，也很不容易。<br />
<br />
她如何觉得有什么不好想的，像我们这样见面，大大方方地聊一会，我觉得行。但我觉得不用在网络这个层面再做什么。<br />
<br />
