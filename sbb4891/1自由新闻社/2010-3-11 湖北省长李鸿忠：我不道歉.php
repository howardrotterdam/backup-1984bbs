李大人本着谦虚谨慎、戒骄戒躁的优良作风，和蔼可亲，耐心细致地回答了媒体记者们的提问。临近结束时，李大人眼睛一亮，突然看到“京报”记者的录音笔，当即感到这是一件高科技的东西。因此出于对科研工作的冲动追求，对发展科技产业的的强烈渴望，李大人提出借用观摩一下。然，可能由于当时会场人多，该访员并未听清李大人的话，而李大人因干革命养成的风风火火的脾气，也没留意到访员的反应，就率性地拿过了录音笔，急冲冲夺门而去，去操劳繁重的革命工作去了。<br />
<br />
可见，事情很清楚，并没有什么可以拿来说三道四的东东。相反，这件事还深刻反映了李大人全心全意为人民服务的，兢兢业业的宝贵作风。<br />
<br />
二、事件细节详解<br />
1、关于小白菜案。有的流言说，是记者提问小白菜案，引起李大人不爽，才生出的风波。这完全是胡说八道，是以小人之心度君子之腹。问小白菜案，李大人怎么会不高兴呢？相反，李大人是兴奋才对。众所周知，耸动海内的小白菜案，在李大人的亲自操持下，在老佛爷、光绪帝的亲自过问下，已经得到了圆满妥善的处理。该案生动地反映了政通人和，海晏河清的盛世景象。如此长脸的事，李大人还担心媒体们不问呢！<br />
<br />
2、关于李大人变脸色。有的流言，还胡编乱造，说其“脸色顿时阴沉” 。这个说法，既可以说靠谱，也可以说不靠谱。为什么说靠谱呢？其时，李大人脸色可能略变了少许，但如上条所说，决不是因为小白菜，而是因为李大人废寝忘食，脾胃虚寒所致。又为什么说不靠谱呢？因为说这种话的人，其实是对我大清千百万官吏的优秀品性还不太了解。我大清官吏哪一个不是人前一张脸，人后一张脸；东边一张脸，西边一张脸；对上一张脸，对下一张脸------所以，“变脸”对俺们来说还不是小菜一碟！世人还说俺们是“二皮脸”，这也太低估俺们了，没有百十八张皮，咋个在这个优秀的队伍里面混罗？<br />
<br />
3、关于录音笔。身为洋务派重镇，李大人一直非常关注科学技术的发展，因而才会生出他向记者急切借用录音笔的佳话。有些流言说李大人“抢”录音笔。大家听听，“抢”这个字用得多么荒唐可笑，不着边际。录音笔即使是新东西，有若干科技含量，但能值几个钱？李大人，一个封疆大吏，一年俸禄多少？犯得着抢一支破笔吗？况且，如果真是李大人稀罕什么玩意，还用得着“抢”吗？全省上下，多少人排着队等着送李大人东西，他老人家连正眼都不想瞧。又，经过半日的揣摩研究，李大人已彻底弄清了录音笔的原理，故当晚就将其归还给该访员了。大家看看，真正用心做事的领导，是要经受多少误解和冤枉啊！所以说，俺们大清为什么这样落后呢？怪就怪人民群众落后哇，觉悟不高哇，愚昧哇！领导们想潜心搞点科学研究，都要被飞短流长，风言风语。改革难啦！<br />
<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;然而，象李大人这样的开拓创新型领导，是不会为歪风邪气所动摇的。他当晚就已拍板，要在武昌镇光谷区上一个录音笔项目，大批量生产该种高科技产品。且为弥除误会，拟延请该“京报”访员出任产品的形象代言人。<br />
<br />
三、上级领导高度重视、亲切关怀<br />
录音笔事件发生后，湖广地方和大清帝国的领导高度重视，亲自过问，亲切关怀。<br />
<br />
1、湖广总督张之洞表态。当日傍晚，湖广总督张公之洞张大人，特别邀请李大人在菊花台共进晚餐。进餐时，张、李两位大人除了深入浅出地交换“两会”的学习心得，认真细致地研究地方重要工作以外，还重点就录音笔事件进入了详细探讨。李大人首先认认真真地汇报了事情的来龙去脉。听取汇报后，张大人表示：“鸿章是个好同志，好兄弟！本事件事出有因，查无实据，不究结果。鸿章同志用心工作，希望发展高科技的急迫心情，组织上及其本人是高度认可的，支持的。然而，由于行为上的夸张，沟通上的误会，信息上的不对称，以及少数别有用心的人，添油加醋，以致事情有一点走样变形。这方面还有请各地牌坊司的同志们加油，努力做好舆论引导和宣传教育工作。不要让广大百姓在背后对我们指指点点，老是说我们既要当个什么子，又想立个什么坊。这就很不好啦。”<br />
<br />
2、恭亲王和醇亲王等国家领导人表态。当日夜，恭亲王奕&#18211;和醇亲王奕X等国家领导人，还在八大胡同某酒楼召见了李大人。两位亲王均表示，赞成张之洞总督的意见。他们还对李大人进行了亲切的慰问和勉励。<br />
（注：关于两亲王与李大人的交谈详情，另见附件。其中，他们不仅就录音笔事件，还就多党制、群体性事件、人亡政息等国内国际重大问题，进行了认真的沟通和研究。由于事涉国家机密，该附件仅限省部级以上领导传阅。另，请各媒体发布本声明时，将本处注解删除）。<br />
<br />
四、社会各界坚决拥护支持李大人<br />
录音笔事件发生后，社会各界先是窃窃私语，继则群情喧哗，纷纷表示坚决支持李大人，坚决看不起某些无聊好事分子。本省相关部门还专门走访了一些人民群众。<br />
<br />
巴山民女邓娇娇说：“一个巡抚大人，说他抢土地、抢房子、抢银子、抢女人，俺还信，就象俺家乡的知县知府们一样。要说他抢录音笔，打死俺也不信！”<br />
<br />
湖北某市周市长表示：“我举双手双脚支持李大人。媒体那个坏呀，俺是体会颇深，去年他们差点就把俺整死了。幸亏俺是死猪不怕滚水烫，对各种言论，不理不睬，装疯卖傻，加上各级大人对俺一直疼爱有加，不离不弃，俺才过了这道槛啦！”<br />
<br />
襄樊民女高莺莺、石首厨师涂远高等均表示：“提起我省、我国各级各位长官大人，俺们就热泪盈眶啊！俺们是恨不能从地下爬起来，向他们表达最最崇高的敬意呀！”<br />
<br />
可见人民群众的眼睛是雪亮雪亮的。<br />
<br />
所谓天地之间有秆秤，是非公道在人心。李大人是大人大量，已不想就此事费心耗神，而是要全身心地投入到我们伟大的改革开放事业当中去。<br />
<br />
这种些微琐事，就让它成为宏大时代潮流中的一个小插曲，一朵小浪花，随滚滚长江东流而去吧！<br />
<br />
我们湖北，是一片具有悠久历史传承的土地。著名的赤壁大战就发生于斯，两晋南北朝以来，更是穿梭金戈铁马，转瞬沧海桑田。多少王朝在这里潮涨潮落，多少家国在这里旋起旋灭。所谓“其兴也勃，其亡也忽”。<br />
<br />
从这些厚重的历史中，我们深知，人民，唯有人民才是历史的真正主人。任何王朝，只要以欺压人民为常态，以愚弄人民为乐事，要想不“人亡政息”，难矣！<br />
<br />
“乱石穿空，惊涛拍岸，卷起千堆雪。<br />
<br />
“来回古今，谈笑间、樯橹灰飞烟灭。”<br />
<br />
谨此惶惶布达天下！<br />
<br />
<br />
<br />
湖北行省巡抚衙门<br />
<br />
庚寅年己卯月戊午日</div>

							
							
							
							
