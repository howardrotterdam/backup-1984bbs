中新网6月30日电 据北京理工大学网站消息，日前，原全国人大常委会委员长、北京理工大学老校友李鹏在西山接见了北京理工大学党委书记郭大成一行。李鹏对母校70周年校庆表示祝贺，并对学校今后发展提出了殷切希望。<br />
<br />
<br />
　　当李鹏在夫人朱琳陪同下来到会客厅的时候，郭大成一行人迎上前去，向李鹏传达了全校4万余名师生的共同问候：“学长好！感谢学长长期以来对学校的关心关爱！”李鹏身体健康，精神矍铄。见到郭大成一行，他十分高兴地说“我是徐老(徐特立)的学生，可以说是理工大的老学长、老校友了，欢迎你们。”<br />
<br />
<br />
　　郭大成首先将学校近年来的工作情况作了汇报。李鹏在听取过程中不时就一些关心的问题进行询问和交流。他说，“我一直都非常关心母校的发展、近年来学校取得的优异成绩和变化，作为一名老校友、老学长，并为学校取得的丰硕成果感到欣慰，更对学校的未来发展充满期待。”随后，李鹏还饶有兴致地询问了学校在北京奥运会、残奥会、国庆60周年、上海世博会等国家重大活动中作出的贡献，了解了学校在国际化办学、人才培养等方面的情况。<br />
<br />
<br />
　　李鹏还深情回忆起了在延安自然科学院学习和生活的情况，回忆起老院长徐特立，以及宝塔山、延河水、杨家岭、王家坪，马兰草造纸、新方法制盐、中央大礼堂建设等等，李鹏对70年前的人和事历历在目、记忆犹新、如数家珍。他还清晰的记得，当年为创办陕甘宁边区的气象台，从延安自然科学院紧急抽调了曾宪波、邹竞蒙等4名同学组建了延安的第一个气象台。他回忆到，自然科学院是我党在抗战时期办的第一所理工科院校，为陕甘宁边区的发展作出了贡献。在延安短短的5年时间里，自然科学院为抗战建国培养了一大批技术专家和革命人才，如中国工程院院士、核动力专家彭士禄，原轻工业部部长曾宪林，原能源部部长黄毅诚，军队高级将领李伦等。<br />
<br />
<br />
　　李鹏对学校今后的发展提出了殷切希望：“学校走过了70年的光荣岁月，为社会主义建设事业桃李芬芳、广育英才。希望学校继续坚持德育首位的育人方针，坚持实事求是的优良办学传统，让延安精神在新的历史时期放射出更加耀眼的光芒。”朱琳也对学校的德育工作非常关心，仔细询问了学生辅导员和班主任开展工作的情况，了解了广大在校学生的思想成长和精神风貌。<br />
<br />
<br />
　　李鹏和夫人朱琳非常高兴地将他们部分新作赠送给母校留念。他们还勉励全校师生要弘扬优良传统，继续开拓前进，为早日实现学校制定的发展目标而努力奋斗。</div>

							
							
							
							
