&nbsp; &nbsp; 为期4天的第28届“首尔国际食品产业大展”，15日在韩国高阳市KINTEX落下帷幕。占据60多个展位的中国展区连日来几乎无人问津，与周围的台湾、日本及美国等展区形成了鲜明的对比。<br />
　　中国展位几成“无人区”<br />
　　<br />
　　本届食品展共有700多家韩国国内厂商和来自32个国家的400多家海外厂商参展。除东道主韩国外，在所有的外国展区中，占据60多个席位的中国展区规模最大。记者发现，该展区尽管展示了各式各样的中国食品，可是连日来几乎无人问津。众多排列整齐的展牌“CHINA”下面，是一片空旷的“无人区”。<br />
　　<br />
　　中国展区的周围，台湾、日本、美国等展区，前来参观、洽谈的客商络绎不绝，台湾鲷、鲈鱼、蚬各类海鲜冷冻食品，鱼丸、鱿鱼丝等加工食品，以及酱油、芝麻酱、雪花冰等冷冻调理食品引起了客商们的注意。<br />
　　<br />
　　率团参展的外贸协会副董事长吴文雅指出，韩国对台湾来说，将是一个值得开发的大市场。<br />
　　<br />
　　“吃在中国”已成过去<br />
　　<br />
　　据几位参展的韩国客商讲，之所以台湾食品越来越受到消费者青睐，是因为那里的食品会更安全。目前在韩国各大商场，各类食品标注着“韩国产”、“美国产”、“澳洲产”……，极少有来自中国的食品，人们常说的“吃在中国，穿在法国，玩在美国，住在英国。”而今天，“吃在中国”早已经成为过去。<br />
　　<br />
　　国际食品展从一个侧面反映了各个国家和地区的饮食文化，而中国展区如今已经退至被各国客商遗忘的角落。究其原因，在各国消费者眼中，自从三聚氰胺、苏丹红、吊白块、福尔马林、孔雀石绿、瘦肉精、……这些添加在各类中国食品中的毒素渗透到国内外的今天，一提起“中国食品”马上让人联想到“有毒食品”。<br />
