													<h2>[娱乐]邢质斌没听说《新闻联播》要改版（图</h2>
						
						
																											<div id="postmessage_156369" class="t_msgfont">“在位”时间最长主播本月初正式退休<br />
<br />
　　昨日有媒体报道，《新闻联播》工作时间最长的播音员邢质斌在今年7月初正式办理了退休手续，虽然以后她亮相《新闻联播》的次数将越来越少，但依然会为一些重大节目担任解说。昨日，邢质斌在接受早报记者采访时证实自己的确已经办完相关手续。但对于“退休不退职”的说法，她表示：“这不完全准确。”央视改版的消息近来颇受外界瞩目，有人猜测，邢质斌的退休也许正预示着《新闻联播》新的开始。不过邢质斌却令人意外地回应说：“没听说要改版。”她表示，“其实每次有重大活动的时候，《新闻联播》都会有一些调整，今年是建国60周年，会推出一些节目，加入新的内容而已。”<br />
<br />
　　东方早报记者 李云灵<br />
<br />
<br />
<br />
 邢质斌主持了《新闻联播》28载。（截屏图 CFP资料）<br />
<br />
<br />
<br />
早年主持《新闻联播》时的邢质斌<br />
<br />
　　曾想当记者离开主播台<br />
<br />
　　作为《新闻联播》的元老级主播，邢质斌给观众的印象似乎一直比较严肃。据悉，她为了做好新闻播报的工作，对自己的要求非常严格，“我只能严于律己，于是我给自己定了一条规矩：不接受采访。”但是在邢质斌退休的消息传出后， 不少观众为今后在《新闻联播》里见不到熟悉的主播而惋惜，邢质斌这才破例愿意接受记者采访。她表示自己对于《新闻联播》也是怀着深厚的感情，并感谢观众的支持。从1981年7月第一次主持《新闻联播》以来，邢质斌在《新闻联播》的主播台上一干就是28年，可谓是这个岗位上工作时间最长的主播。在1980年代初，面对罗京、张宏民、杨柳、王宁等大批后起之秀，邢质斌的心里充满了压力，她曾想从《新闻联播》退下来。邢质斌曾透露，她也有过思变的时候，转行一度成为她的一块心病，“20世纪90年代初，我想做一名记者，希望能调到采访部门去，冲到重大事件的第一现场，但我的申请总是石沉大海，后来我又喜欢上了《夕阳红》节目，自以为这次应该不会再错过了，可是每当下决心的时候，我都会心怀不舍，似乎不能离开《新闻联播》。”<br />
<br />
　　知情人说两年前就“退”了<br />
<br />
　　按照央视的退休政策，作为专家型的主持人，到了60岁就该办退休手续。昨日记者从央视一位知情人士口中得知，邢质斌早在两年前就已经办理了退休手续，这位知情人士回忆说，2006年年底，邢质斌获得金话筒奖的时候说了一句，“我觉得得不得这个奖其实不是很重要，既然在这个岗位上，我就不辜负领导和观众对我的厚爱。给了我这个奖，就是善始善终吧。” 那个时候邢质斌已经59岁了，按照规定，邢质斌第二年就向央视提交了退休申请，由于她是台里的播音骨干，而当时《新闻联播》的主播青黄不接，出于节目的需要，《新闻联播》栏目组以“节目聘”的形式将邢质斌返聘，所以这两年观众仍然在荧屏上能够看到她。今年邢质斌已经62岁了，早已超过了退休年龄。据了解，今年因病去世的《新闻联播》主播罗京就一直尊称她为二姐。在罗京的追悼会上，邢质斌流着眼泪送了这位老搭档最后一程。也就是在那以后，她向台里再次提出“退休”，并在7月初完成了所有手续。<br />
<br />
　　对康辉李梓萌非常赞赏<br />
<br />
　　作为《新闻联播》节目组的老大姐，邢质斌的业务水平得到同仁认同，她的人缘也是出了名的好。据悉，《新闻联播》的同仁还为其举办了一场欢送仪式。但是对于有媒体报道她在《新闻联播》的舞台上“退休不退职”的说法，邢质斌表示：“这不完全准确。”她对《新闻联播》的“新面孔”康辉和李梓萌的表现则大加赞赏，“他们现在已经完全有能力做好了。” 至于是否会接下其他新闻节目，或者担任重大节目的解说工作，“以后还会怎么样还不知道呢。”她说。针对颇受关注的央视改版的消息，以及《新闻联播》也将改头换面的传闻，邢质斌却表示：“我没有听说要改版啊，其实每次有重大活动的时候，《新闻联播》都会有一些调整，今年是建国60周年，会推出一些节目，加入新的内容而已。”<br />
<br />
　　央视播音员大事记<br />
<br />
　　● 1976年7月1日，北京电视台第一次试播全国电视新闻联播节目，1978年1月1日正式启用 “新闻联播”名称，第一代播音员是李娟、赵忠祥。1978年5月1日，经中共中央批准，北京电视台更名为中央电视台。<br />
<br />
　　● 1979年底，《新闻联播》首次出现播音员图像。<br />
<br />
　　● 1983年开始，卢静、罗京、张宏民、李瑞英等进入《新闻联播》。<br />
<br />
　　● 1987年，《新闻联播》由一人主持改为两人主持，节目结束时，打出编播制作人员名单。<br />
<br />
　　● 1996年1月1日，《新闻联播》从录播改为直播。但直播方式和即时播报的普遍采用，已经开始冲击《新闻联播》的首发特权。<br />
<br />
　　● 2006年6月，李梓萌、康辉加入《新闻联播》。<br />
<br />
　　● 2007年12月，海霞、郭志坚亮相《新闻联播》。<br />
<br />
　　（早报综合）<br />
