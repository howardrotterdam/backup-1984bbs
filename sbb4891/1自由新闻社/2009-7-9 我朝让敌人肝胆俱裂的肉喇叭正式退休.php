													<h2>国家喉舌果然地位非常  邢喇叭退休不退职仍将获重用</h2>
						
						
																											<div id="postmessage_156791" class="t_msgfont">CCTV设宴欢送邢质斌 仍将获重用退休不退职<br />
<br />
2009年07月10日08:00<br />
<br />
<img src="http://img.hexun.com/2009-07-10/119480965.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　日前，有媒体报道称，央视著名播音艺术家、《新闻联播》金牌主持人邢质斌，已于今年7月初正式办理了退休手续。昨天，一位不愿透露姓名的央视知情人士确认了该消息，称邢质斌于6月初就向央视有关部门递交了退休申请，央视在考虑到她的年龄、身体状况以及各方面综合因素后，于当月底正式批准了邢质斌的退休申请。昨日，央视领导、播音组同事以及《新闻联播》编导们集体为邢质斌举办了欢送会。<br />
<br />
&nbsp; &nbsp; 央视摆宴6桌欢送<br />
<br />
　　昨天中午，一场气氛颇为凝重的聚会在与央视大楼邻近的恩菲大厦内某酒楼举行，除央视现任台长焦利未到场外，央视领导以及重组后的新闻中心高层悉数到场为邢质斌送行。作为在《新闻联播》工作时间最长的播音员，邢质斌今后将不再担任《新闻联播》播音工作。参加欢送会的还有李瑞英、胥午梅等同在播音组的主持人，和曾经与邢质斌一同奋战在《新闻联播》栏目的编导们。 <br />
<br />
　　据悉，当天的欢送会一共设宴六桌，席间央视领导回顾了邢质斌为央视做出的重大贡献，称赞她担任主播期间，形成了端庄大方、字正腔圆、严肃认真的播音风格。同时，也肯定了她平日在工作中对新人的帮助和扶持，对其他同事的关心和照顾。因为邢质斌在同事中人缘很好，大家都舍不得她走，欢送会上不少同事举起酒杯向她敬酒，同时还表达了希望她继续留下来工作，指导年轻人如何播音的心愿。<br />
<br />
　　重大节目还会出马<br />
<br />
　　1981年7月，邢质斌首次与赵忠祥以男女主播、相互搭档的形式正式出镜《新闻联播》。屈指算来，邢质斌已经在《新闻联播》这个主播台上工作了整整28个年头。按央视相关人事规定，作为专家型的主持人，60岁就该办理退休手续。而1947年11月出生的邢质斌，今年已经过了61岁。早在2006年，就有邢质斌将要退休的消息传出。2006年6月初，央视安排了较为年轻的新闻主播康辉、李梓萌首度登上《新闻联播》主播台，许多媒体开始纷纷猜测“邢质斌将会退休”，但都未获得央视的证实。后来海霞、郭志坚相继加盟《新闻联播》，在“新老并进”的原则下，康辉等主持人逐渐在磨练中成长起来，直至今天能独当一面，才给了老一辈主播邢质斌退休的机会。 <br />
<br />
　　昨天，央视《新闻联播》节目组证实，邢质斌今后将不再担任《新闻联播》主持人及与之相关的播音工作。至于她退休之后是否有“新面孔”登上主播台，节目组表示目前尚未接到上级通知。据悉，随着6月5日罗京的去世，以及现在邢质斌的退休，《新闻联播》的主持人队伍目前固定为李瑞英、李修平、张宏民、王宁、李梓萌、康辉、海霞、郭志坚。 <br />
<br />
　　知情人士透露，由于邢质斌主持新闻节目经验丰富，功夫过硬，央视还会重用她。她将和赵忠祥一样，退休不退职，一些重大节目的解说还会请她出马。 <br />
