　　把“国字脸”的活继续干好干活<br />
<br />
　　上海大学教授 顾骏&nbsp; &nbsp;<br />
<br />
　　<br />
<br />
　　罗京的去世和邢质斌的退休，全国人民久已熟悉的两位央视《新闻联播》主播离开荧屏，似乎印证了纷纷扬扬的央视改版之说。确实，人员交替与世事更迭常会形成出人意外的契合，因为诚如20世纪著名科学哲学家库恩所言，在科学史上，一项原有的理论被否弃，常常既不是因为发现了新证据，从而证伪了它，也不是因为建立了新理论，从逻辑上驳倒了它，而简单只是因为相信这种理论的一代人离开了历史的舞台。理论与一代人往往共享了同一个生命历程。<br />
<br />
　　这样的说法尽管令人遐想，但真要拿来解释社会文化变迁，显然过于玄虚。现在关于央视改版众说纷纭，方方面面不少是基于各自关于央视的地位、新闻的定位和中国社会未来发展的趋势等或大或小的构想所提出来的理想形态，其中不乏彼此冲突甚至自相矛盾之论。诸如这一代主持人的“国字脸”过于严肃，《新闻联播》题材过大之类的说法，其内含的预设显然禁不起学理推敲和国际比较。推动央视改版的核心原因只有一个，那就是面向国内和国际的影响力消长。如果我们把“国字脸”不是看作主持人的形象，而是看作国家的代表，那只要有足够的观众愿意看，愿意信，“国字脸”不但不成问题，而且应当做得更好。坦率地说，今天央视那么多频道和栏目，不是“国字脸”多了，而是少了，太多不符合国家台要求的内容、风格、主持人和广告张扬于中央电视台上，才是真正的不合时宜。国家需要国家的形象，尤其在中央电视台上！<br />
<br />
　　然而，国家的形象不能停留在直观的视觉上。如果“国字脸”仅仅传递国家的威严，传递信息的不容置疑，那只会成为正在步出历史舞台的那一代人曾经的熟识。在公民知情权、监督权和表达权空前落实，各种信息渠道展开激烈竞争，电视遥控器决定主持人的被观看机会，网络互动牵着其他媒体的鼻子走的现实情况下，不能被观众锁定的电视台和频道，到底需要怎样的主持人，主持人到底需要怎样的形象，并没有太多的讨论价值。<br />
<br />
　　可以肯定的是，随着老一代主持人的离去，后继的主持人必然带着新一代的特征，出现在荧屏上，他们将不但继续代表国家的形象，而且将在国家与国民新的关系样式的背景上，展现国家级电视台代表国家的新方式。不以普通电视台的标准要求自己，努力做好“国字脸”的那份活，最大限度地提升世界影响，树立高度的社会公信力，是央视无论改版与否，都应该在主持人之间薪火相传的使命。<br />
