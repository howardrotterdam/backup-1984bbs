													<h2>我朝让敌人肝胆俱裂的肉喇叭正式退休</h2>
						
						
																											<div id="postmessage_154422" class="t_msgfont">央视《新闻联播》金牌主持人邢质斌低调退休(图)<br />
2009年07月09日 08:01　来源：四川在线－华西都市报<br />
<br />
<img src="http://www.chinanews.com.cn/yl/yl-ypkb/news/2009/07-09/U149P4T8D1767086F107DT20090709080143.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
<br />
　　昨(8)日，记者从央视内部一位知情人士处获悉：央视著名播音艺术家、《新闻联播》金牌主持人邢质斌，于今年7月初正式办理了退休手续。据悉，邢质斌将退休不退职，虽然以后亮相《新闻联播》的次数将越来越少，但依然会为一些重大节目担任解说。<br />
<br />
　　送别罗京后悄然办理退休手续<br />
<br />
　　邢质斌与刚去世不久的川籍著名主持人罗京亲如姐弟，也是《新闻联播》多年的老搭档。据了解，罗京一直尊称邢质斌为二姐。今年6月5日，罗京是在邢质斌等几名好友的送别下离开人世的。<br />
<br />
　　按央视退休政策规定：作为专家型的主持人，到了60岁就该办退休手续。掐指一算，邢质斌今年已61岁了。在送走罗京小弟几天后，邢质斌于6月底正式递交了退休申请。到了7月初，央视人事部门为邢质斌办理了正式退休手续。邢质斌在同事中人缘很好，大家都舍不得她走，还希望她继续留下来工作，指导年轻人如何播音。所以，目前《新闻联播》节目组还没有为邢质斌办欢送会。<br />
<br />
　　经历很传奇 她曾是一名公社广播员<br />
<br />
　　1981年7月，邢质斌首次与赵忠祥以男女主播、相互搭档的形式正式出镜《新闻联播》，多年来，她更以端庄大方的形象、字正腔圆的播音获得观众的认可。昨天，谈到老搭档邢质斌，赵忠祥笑道：“在《新闻联播》几代播音员中，邢质斌可以说是一个传奇人物。”<br />
<br />
　　据赵忠祥回忆：“1973年，当时的中央电视台叫北京电视台，只有我和沈力、吕大渝三名播音员。随着新闻信息量增大，相当缺新闻播音员。有一天录音科科长宋培福到北京郊区大兴县的岳父家，突然在门口听到了大兴县县广播站传出了一个特别的女声，音质的力度，抑扬顿挫的感觉，让他大感意外。宋培福立即通过大兴县广播站，找到了那个播音员，她就是邢质斌。邢质斌告诉宋培福，她1964年毕业于北京师范大学附属高中，毕业后，下到大兴县插队，后又到县广播站播音。不久，宋培福就为台里推荐了邢质斌。当时，我见到邢质斌时，她才20多岁，短发、文静，不善言辞。不到两个月，邢质斌就办理了调动手续。从1974年开始，我就和她一起主持央视新闻节目。邢质斌的播音才华很高，她的音质质感属一流。邢质斌是在《新闻联播》工作时间最长的播音员，其中最著名的是分别现场解说了1984年和1999年的国庆阅兵式。”<br />
<br />
　　退休不退职 将为重大节目担任解说<br />
<br />
　　今年已67岁的赵忠祥，在办理了退休手续后，越来越红。不仅多家地方台抢他，还应邀到东方卫视与台湾大牌主持人吴宗宪主持娱乐节目，目前，央视的《动物世界》、《人与自然》仍由他主持。作为与赵忠祥齐名的邢质斌，办理退休手续后还会不会在央视主持节目？记者多方联系邢质斌本人，但《新闻联播》的工作人员表示：邢质斌做人低调，从不接受记者的电话采访。<br />
<br />
　　最后，该知情人士告诉记者：“作为《新闻联播》的金牌主持人，邢质斌今后亮相《新闻联播》的次数将减少，但因她主持了几十年新闻节目，经验丰富，功夫过硬，央视还会重用她。她将和赵忠祥一样，退休不退职，一些重大节目的解说还会请她出马。”<br />
