													<h2>本人N年前涉及李省长的一个贴子</h2>
						
						
																											<div id="postmessage_507439" class="t_msgfont">市委书记给自己立军令状吓着了谁？<br />
网友：一又二分之一<br />
<br />
<br />
--------------------------------------------------------------------------------<br />
<br />
宁夏新闻网　<a href="http://WWW.NXNEWS.NET" target="_blank">WWW.NXNEWS.NET</a>　　2005-4-15 10:30:00<br />
<br />
　　3月27日下午，广东省委常委、深圳市委书记、市长李鸿忠在深圳市委工作会议上，当着在场的1100多名与会人士，就廉洁自律、公道正派，自己给自己立了一个军令状――“在深圳，请首先监督我。”并对采访的记者说，“今天我郑重立下了军令状，不仅要请党内各位同志监督我，也欢迎全市人民、社会各界和新闻媒体监督我。”据记者报道，李鸿忠真诚的态度，“深深打动了每一个人”。<br />
<br />
　　书记大人的军令状，且不说谁来监督，也不说如何监督，单是这种自己给自己下达军令状的做法就很让人生疑：人真的能自己吓着自己吗？<br />
<br />
　　事实上，这种通过自我约束规范个人行为的做法，就是“德治”，又称“修身”。这也正是儒家的先贤为我们遗留的“内省”式工作路径，或“吾日三省吾身”，或“见贤思齐，见不贤而内省”，或自律、自警、自醒、自制等，而落脚点无非都是想依靠个人道德观念来驱动自己的行为。近年来，我党先后开创的诸如“贤内助”看管丈夫、孩子监督家长之类的“反腐新举措”也属于此类，只不过是诉求的是家庭道德，较之个人道德，也可算是与时俱进了一回。<br />
<br />
　　大多数社会学家仍然认为，道德，哪怕强大到能够“治国”的道德，其激励和约束力量，也只属于制度中“无形的”的“非正式的”部分。而就一种制度的有效性来说，“正式的”和“非正式的”部分可能都是不可缺少的。并不排除有时候，“无形的”和“非正式的”规则要比“有形的”和“正式的”规则更有力量。但是“正式的”制度确能够为人们提供确定的行为准则，减少随意性。因此，越是“正式的”制度越是能够有效地约束或监督或激励人们的行为。<br />
<br />
　　想当年，孙悟空同志如果只是自己给自己戴上了一个紧箍圈，籍此想让该同志“始终保持先进”，从此不再闹上天宫，事实肯定会“走向良好愿望的反面”。现实生活中，榜样的力量固然是无穷的，但是也可能一无所用。今天的社会，真正需要的是制度创新和制度建设，而不是领导的表率或榜样的一再推陈出新。&nbsp; &nbsp;<br />
