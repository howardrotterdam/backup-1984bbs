环球网实习记者李宗泽报道，据《印度时报》4月11日消息，中国将在珠穆朗玛峰附近建造一所命名为“平安”的机场，为登山爱好者提供便捷的交通设施，致力将珠穆朗玛峰打造成为登山爱好者的圣地。<br />
