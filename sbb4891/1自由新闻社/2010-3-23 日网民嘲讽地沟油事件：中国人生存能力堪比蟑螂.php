&nbsp; &nbsp; 近日，一条“中国人一年吃掉300万吨地沟油”的新闻引发了中国媒体的热议，这则新闻也被日本媒体所关注。一直以来，日本人都很关注从中国进口的食品的安全问题，现在“地沟油”新闻的出现，又让日本人议论纷纷，其中不乏一些激烈言论。有日本网民呼吁“不要进口中国食品”，甚至讽刺中国人“有和蟑螂一样的生存能力”。<br />
<br />
&nbsp; &nbsp; 近日媒体报道说，中国餐饮业一直存在着“地沟油”黑幕，很多大饭店是地沟油的原料提供者，并且地沟油毒性被证明是砒霜的100倍。这些新闻被日本的Record China等网站转述报道。在日本网民经常参与留言的日文雅虎网站以及日本本土的知名门户网站Livedoor上，日本网民纷纷留言表示对中国人食用地沟油感到震惊，甚至施以讽刺言论。<br />
<br />
　　日本网民纷纷留言表示对中国人食用地沟油感到震惊，甚至施以讽刺言论。日本网民“Blackchibiko”留言：“我在超市看到标有‘中国产’字样的东西，就马上放回去，还要把手消毒一下。中国因为人口众多所以就放任自流，我死也不愿意去中国旅游。”网民“hidetaroh”说：“说吃中国产的油炸食品，就有10分之1的概率摄入砒霜。”网民“bakaseifu”则揶揄说：“如果发生核战争的话，最后活下来的就只有蟑螂和中国人。是不是中国人现在就在为此而锻炼身体的耐性吧！相反因为日本人太爱干净了，可能马上就会死掉。”<br />
<br />
　　还有网民表示“坚决不要再从中国进口任何食品”。网民“kokusuihozon”说：“由于都在使用中国食品，所以中国食品也进入日本。随之蜂蜜、盐等的假货也进入了日本。因为价格便宜，所以从中国进口有毒蔬菜子什么的也是一样的道理，贪便宜不顾后果，最后反而得不偿失。”网民“Cornelius hr”则留言：“即使没有直接从中国进口那种油（地沟油），但用那种油加工的食品等说不定从中国大量进口了。买加工后的食品一定要去确认一下生产国家。要不然后悔莫及呀。”<br />
<br />
