镜头背后的故事回顾： <br />
<br />
来自南方周末的报道： <br />
<br />
　　2009年10月24日，长江大学3名大学生何东旭、方招、陈及时为救两名落水少年，献出了宝贵生命。令人心寒的是，在英雄遗体打捞时，面对同学们的“跪求”，个体打捞者不仅不为所动，而且挟尸要价，一共收取了3.6万元的捞尸费! <br />
<br />
　　在年轻的学生们舍己救人的英雄行为感动中国之时，“挟尸谈价”的捞尸人和“见死不救 <br />
<br />
　　”的渔民也顿成千夫所指。 <br />
<br />
　　然而当天，真实的情况是什么？都有谁参与了当天下午对溺水大学生的救援？渔民是否拒绝救人？在打捞遗体的过程中，“挟尸谈价”的真实过程又是如何？ <br />
<br />
　　诸多事实极具争议又有待澄清。 <br />
<br />
　　一张照片：“手牵绑尸绳谈价” <br />
<br />
　　10月24日下午4点05分,荆州捞尸人王守海将第二具大学生遗体打捞出水。溺亡者是湖北长江大学学生方招。 <br />
<br />
　　当时王守海立在船头，一手拉着一根尼龙绳，绳子的尽头是一把钩子，跟钩住的那具年轻的遗体一起隐没在水面下。王守海的背后是捞尸队的一名同伴，他同样拉着一条绳子，系住遗体的手腕。捞尸的小船在缓缓地靠向岸边。岸上等待着的，是溺水者陷于绝望和惊慌中的同学们。 <br />
<br />
　　这一画面被赶到的记者拍下，数天后辅以“捞尸人手牵绑尸绳谈价”的报道刊出，之前网络上已风传“渔民不救活人只捞死人赚钱”的网帖。当地捞尸人和渔民成为众多网友的批判对象。 <br />
<br />
　　疑团一:有没有“见死不救” <br />
<br />
　　10月24日14点，从江对面的埠河镇过来卖鱼的陈选德、陈恒云正在渔船上睡觉。 <br />
<br />
　　23日晚，他们打了一夜的鱼。早上天亮，便开船到宝塔湾卖鱼。遇到大学生求救之前，陈选德他们的鱼大部分已经出售了，盼着天黑前，剩下的鱼也能顺利卖完。两人正在自己的船上补昨晚的觉呢。 <br />
<br />
　　事发当天(10月24日)下午，当三名救人的大学生在水中失踪后，周围的同学们就开始四处求救。“几个女生就像疯了一样。给当时在场的每一个人下跪。求对方下水救人。”冬泳队员韩德元回忆。 <br />
<br />
　　学生高阳在第一时间分别向110、119以及120拨打了求助电话。但包括海事局、长江水上公安局、长江航运警方，都来看了看就走了，他们都没有“专业设备”，“干不了打捞的事”。 <br />
<br />
　　学生们又去央求江边的渔民陈选德和陈恒云。当时，这两个渔民因打了一夜的鱼，正在自己的船上补昨晚的觉呢。“我们只能救水上的。”陈选德说，“捞水下的，我们没有设备。有设备也不能捞。” <br />
<br />
　　在面对记者时，没什么文化的两位渔民要解释当天的困境显得非常费力。他们翻来覆去申辩的重点：一个腰有伤，年纪大了，不敢下水。一个根本不会游泳;那么冷的天，5分钟人已经“没了”；他们没有下水捞尸体的权利。 <br />
<br />
　　不难想象,在当时绝望混乱交织的情况下，面对激动的女生们，陈选德和陈恒云多么难以解释清楚。在被渔民拒绝后，女生们喊着“你们不救我们自己救”，便要往江水里冲，但被人拦了下来。 <br />
<br />
　　冷酷的现实是，在短短的5分钟之后，方招、陈及时和何东旭沉入冰冷的江底，“救人”已经失去了意义，按照常年行走江边的渔民和冬泳队员的理解，这时候救援已经转变为“打捞”了。 <br />
<br />
　　疑团二：有没有“牵尸谈价” <br />
<br />
　　“三具尸体，都是我捞上来的。”荆州捞尸人王守海回忆说，“第一具穿的红衣服，第二具是白衣服，第三具是蓝衣服。”“红衣服”是陈及时，“白衣服”是方招，“蓝衣服”是何东旭。 <br />
<br />
　　事发当天下午两点半，捞尸人王守海接到同村陈兴的电话，说宝塔湾有尸体捞。王守海带上了自己的钩子。系在一条长长的尼龙绳上的钩子，以前用来在长江里捞中华鲟这样的大型鱼类；大鱼很早以前就没有了，钩子现在只用来捞尸体。 <br />
<br />
　　打捞三具尸体，捞尸人王守海赚到了530元钱。捞尸队的老板陈波给了8个捞尸人一共4000块钱。 <br />
<br />
　　此前，捞尸队老板陈波跟赶到现场的长江大学老师在沙洲上的阳伞下面“谈了”价格。长江大学的老师回忆，“实际上没什么谈的空间”，江面上打捞尸体的价格是“定价”，“谈价”很快就结束了。一具尸体1.2万元，先交钱，后打捞。 <br />
<br />
　　老师和学生们身上带的现钱不多，一共凑了4000块，先交给了陈波，在得到陈波允许后，王守海们开始了搜寻。“红衣服”陈及时的遗体在3点半左右出水。又半个小时后，“白衣服”方招也被找到。 <br />
<br />
　　那幅流传于网络的照片正是在此时被拍下，王守海立在船头，带着方招的遗体向岸边靠拢。没有一个当天的学生能够回忆证实，执行打捞任务的捞尸人曾经在那一刻“手牵绑尸绳与学生谈价钱”。 <br />
<br />
　　但王守海不知道的是，在他背后沙洲上的阳伞下面，捞尸队老板陈波已经跟长江大学的师生们产生了争执。在最初的4000块钱之后，剩余的3.2万块钱还没有拿来，陈波不干了，按照“讲好的规矩”，要拿到现钱才干活儿。 <br />
<br />
　　看不到钱，陈波给船上的王守海打了一个电话，让他停下来。在把方招的遗体放到岸边后，王守海们驾船退到了沙洲边上，他们蹲在船上抽烟，等着老板陈波的下一个指示。 <br />
<br />
　　打捞工作就此停顿。半个小时后，钱送到了，陈波下令，王守海们再次开始搜寻。5点，“蓝衣服”何东旭，最后一具遗体，终于被打捞上岸。 <br />
<br />
　　据王守海介绍，陈波的“荆州市长江水上打捞有限公司”控制着长江荆州段所有的尸体打捞。通常，都是陈波通知他的哥哥陈兴，再由陈兴负责找人手、找设备。“打捞公司没有船也没有钩子，但是陈波有关系。”王守海说。 <br />
<br />
<strong>　　一具尸体可以带来1.2万元以上的收入。捞尸队老板陈波和救人的冬泳队曾多次发生纠纷。据保持着冬泳救人纪录的队员柯利沙说，陈波曾警告他：你不要断我财路。 <br />
<br />
　　而陈选德、陈恒云以及其他多名渔民均告诉记者，陈波曾警告他们，不许捞荆州段的尸体，否则，“砸你们的船，烧你们的网”。他们顺从地从不参与打捞尸体。 </strong><br />
<br />
　　校方证实:渔船参与了救人行动 <br />
<br />
　　事发当天下午3点半，在第一具遗体出水后，渔民陈选德、陈恒云驱船回到了埠河的家中。他们不知道的是，“见死不救”和“只捞死人不救活人”的恶名已落到了自己身上。 <br />
<br />
　　10月28日下午，陈选德和妻子再次开着他们破旧的渔船来到宝塔湾卖鱼。“船刚靠岸，几个人从岸边冲到渔船上，我脸上就挨了三巴掌。”70岁的陈选德说。随后，渔网被拉到岸上，他老伴则紧紧地护住渔网。惊恐中，陈选德已经忘了申辩。事实上，他根本没弄清楚到底出了什么事。直到听见有人骂：“你这个见死不救的，还敢来荆州卖鱼！” <br />
<br />
　　“我看到渔民们用竹竿帮我的同学了。”大学生李立科证实。李立科的说法与韩德元的说法形成了印证。“渔船的老板确实拉了我们一把。”而长江大学宣传部的消息称，事后，收集学生们的信息证实，当时有6人是从渔船上上岸的。渔船确实参与了救人行动。 <br />
<br />
<br />
