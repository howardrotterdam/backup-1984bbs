													<h2>大学官员撰文呼吁取消《挟尸要价》获奖资格</h2>
						
						
																											<div id="postmessage_789857" class="t_msgfont">核心提示：《挟尸要价》获得中国新闻摄影最高荣誉“金镜头”的年度最佳新闻照片奖。长江大学党委宣传部部长撰文称该图片系造假，而事实真相并非图片所展现的。他建议有关部门核实照片的真实性和新闻性，呼吁取消《挟尸要价》获奖资格。<br />
<br />
<img src="http://img2.cache.netease.com/cnews/2010/8/20/20100820094111b3fea.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
黄河新闻网8月20日报道&nbsp;&nbsp;今天，几乎在各大网站的首页都公布了中国新闻摄影“金镜头”年度新闻图片颁奖的消息，其中作品《挟尸要价》以全票赢得了本年度最佳新闻照片奖，成为颁奖典礼上的焦点。&gt;&gt;&gt;查看获奖作品<br />
<br />
2009年的10月24日下午，长江大学的15名学生在长江宝塔湾河段救起两名少年，不幸的是有3名大学生被江水吞噬。大学生的英勇行为感动了中国，温暖了社会，受到了万民的称赞。众多媒体云集荆州，争相报道英雄的事迹，但也有的媒体“另辟溪径”，他们的到来是“为了寻找英雄背后不该发生的”事件，甚至误读照片，制造假新闻，《挟尸要价》就是其中最明显的一例。但就是这样一张照片却能在全国屡屡获奖，昨天(8月18日)又被公布获得中国新闻摄影最高奖。<br />
<br />
其实，这张照片在《华商报》发表、经《新京报》转载后，《南方周末》记者杨继斌经过调查2009年11月4日在湖北荆州发出了《大学生救人溺亡隐情调查：“挟尸要价”另有其人“见死不救”渔民被冤》的调查报道，并被广泛转载；媒体人马文娟2009年11月8日撰写了《从大学生救人事件的报道看媒体责任》一文，提出有关媒体对大众的误导要承担责任；长江大学宣传部长李玉泉也向新闻媒体通报了事实真相，并在2009年11月16日在自己的署名博客上发表了《“牵尸谈价”：不能不说的事实真相》的文章。就此，舆论和大众基本都已了解了事实真相。<br />
<br />
《华商报》和《新京报》对照片的解读是：船头着白色衬衫者陈某与老师、学生牵着尸体谈价。事实是：在打捞中，两条船共同作业，捞到后要迅速地用绳子拴着手或脚，避免再次滑落水中，船太小，在深水处不容易把尸体捞上来，要牵引到岸边再捞起。着白色衬衫者手牵尸体挥舞手臂是指挥岸上的人们配合将尸体打捞上岸。但误读的结果使“牵尸靠岸”在《华商报》和《新京报》的报道中变成了“牵尸谈价”。而且，该照片在《华商报》和《新京报》发表时署名为“真真”，现在获奖人为“张轶”，我们有理由怀疑，这两个名字可能用的都是假名。<br />
<br />
追求事件的真相是每一个没有亲临现场的人们所期望的，也是媒体代言大众应该向大众转达的。亲临现场的人们如果对某一个场面产生了误读，可能只是导致自己对事件的了解不全面或不理解，但媒体的误读就会误导社会大众。《华商报》和《新京报》对照片的误读误导了全国大众，各种媒体口诛笔伐，人们在网上发贴咒骂“渔民”的没有人性，甚至咒骂“荆州人”的“冷血”。尽管调查报告已经公布，有关方面已经澄清，但“非事实”的造假新闻在社会中一直传播并引起人们对这个社会或人性的“失望”。<br />
<br />
两船合力将尸体牵引到岸边，捞起一具送到岸边一具，再打捞下一具,荡漾的水波显示船的划动。据有人证实，这名捞尸人不姓陈，而是叫王守海。<br />
<br />
渔民陈凯说“捞到人，我们每船每次能得500元，捞不到就是200元，甚至有时给包烟。”可见，如果谈价，也不是由船上从事打捞的这些人。因为我们对“渔民见死不救”的误传，已经使本来应该列入“救人集体”的两个老人现在不敢出江，不敢见人，不敢与人打招呼。老人在宝塔湾卖鱼。他被人打了三个耳光，腰上踹了一脚。“无数的砖头瓦砾向我扔来，几百人围过来，他们大声指责谩骂，有人喊打死他，打死他，这个没良心的老东西，见死不救的畜生。”最后，船被砸了，渔网也被烧了。那么，因为我们对照片的误读而使这四个老人的生活举步维艰，善良的人们又于心何忍？<br />
<br />
在8月5日公布《挟尸要价》照片获得全国摄影艺术展记录类银质奖之后，长江大学宣传部已经向“全国摄影艺术展评委会和各位评委”发出了“就张轶《挟尸要价》照片获全国摄影艺术展记录类银质奖致评委会的公开信”。今天，就该照片获得“中国新闻摄影最高荣誉”，我们还要发出呼吁：<br />
<br />
1、&nbsp;&nbsp;建议有关主管部门核实照片的真实性和新闻性；<br />
<br />
2、&nbsp;&nbsp;建议组委会和评委会撤消该照片的获奖资格；<br />
<br />
