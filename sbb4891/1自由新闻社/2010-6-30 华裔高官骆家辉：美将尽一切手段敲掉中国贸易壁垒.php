&nbsp; &nbsp; “我们随时准备在对话失败的情况下将中国诉诸WTO。”<br />
&nbsp; &nbsp; “我们将尽一切手段敲掉中国的贸易壁垒，扩大美国对华出口。”<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp; 6月23日，在美国参议院财政委员会就美国对华贸易政策举行的听证会上，美国商务部长骆家辉和贸易代表科克气势汹汹。这距离二十国集团（G20）多伦多峰会召开还有两天的时间，距离中美第二轮战略与经济对话（S&amp;ED）结束不到一个月。<br />
&nbsp; &nbsp; 骆家辉和科克的强硬立场和美国政商界的压力息息相关。上述财政委员会重要成员、美国民主党参议员舒默一直致力于推动《2010年汇率监管改革议案》，就汇率问题向中国施压，舒默自称该议案已经赢得大部分参议员的支持。一位美国商会负责人向本报记者表示，不少在华美国企业遭遇“非国民待遇”，5月中美S&amp;ED没能成稿打消美国商界的忧虑。<br />
&nbsp; &nbsp; 其实，美国的单方面指责并不公正。<br />
&nbsp; &nbsp; 峰会前夕，中国出台了众多有利于国际经济平衡的经贸政策。新华社6月20日、23日先后报道说，中国人民银行决定进一步推进人民币汇率形成机制改革，中国财政部将取消部分钢材等406个税则号商品的出口退税。而债台高筑的美国政府却没有提出任何解决自身财政难题的可行方案，中国美元资产风险依旧。<br />
&nbsp; &nbsp; 汇率：再施压本报记者从美国政界知情人士处了解到，美国政府内部曾达成一致，除财政部长盖特纳外，其余官员不得就汇率问题发表公开言论。骆家辉5月访华前对媒体相关提问，一律以“询问财政部”回应。<br />
&nbsp; &nbsp; 有分析人士指出，美国政府官员前几个月在汇率问题上噤声，是因为政府认为汇率问题需要与中方“静悄悄沟通”方能达到结果。<br />
&nbsp; &nbsp; 中国外交部发言人秦刚17日也表示，在G20多伦多峰会上讨论汇率问题并不适合。<br />
&nbsp; &nbsp; 然而，6月23日，骆家辉意外表态：“人民币币值被低估，这是毫无疑问的。”他进而表示，奥巴马总统会在G20峰会上继续和中国领导人就人民币汇率问题展开磋商，财政部长盖特纳也会密切跟踪汇率问题。<br />
&nbsp; &nbsp; 国会议员的表态更加直接。“中国想把人民币汇率拉下G20谈话桌是不可能的。我们就是要在G20上谈人民币。”新泽西州参议员Robert Menendez在听证会上这样表态。<br />
&nbsp; &nbsp; 骆家辉当天暗示美国商务部可能就汇率问题采取“极端措施”保护美方利益。他说，在法律要件齐备的前提下，商务部在涉华贸易救济纠纷中，“展开汇率补贴调查”<br />
&nbsp; &nbsp; 美国商务部目前正对中国输美铜版纸展开“反倾销反补贴”调查，此案美方原告要求将人民币币值被低估部分计算为中国政府补贴，并征收相应反倾销关税。而美国商务部历史上还从未将人民币汇率偏差列为征收反侵销税的依据。<br />
&nbsp; &nbsp; 本周，人民币对美元汇率波动不大，周二从6。81小幅上扬到6。79后，迅速在周三回落到6。82。<br />
&nbsp; &nbsp; 除了汇率，骆家辉23日公开质疑了中国市场开放政策，“中国国有企业对行业的控制在增加，政策对市场的影响在加深”，美国需要确保美企对华出口的产品和服务与中国竞争者享有同等待遇，需要确保在华美国企业享受“国民待遇”。<br />
&nbsp; &nbsp; 骆家辉当天再次把矛头指向中国的出口创新政策。<br />
&nbsp; &nbsp; 2009年11月，中国科技部联合发改委、财政部开始在全国范围内进行自主创新产品认定工作，最终形成《国家自主创新产品目录》。未来财政部将会同有关部门，依据政府采购政策的需求和实际情况，在该产品目录基础上制定《政府采购自主创新产品目录》。<br />
&nbsp; &nbsp; 由于政府采购的市场规模预计高达5800亿美元，美国商界从一开始就反对这一政策。中国美国商会近日所做的一项调查显示，23%的在华美国企业对经商环境表示担忧，这一比例比去年增加5个百分点。<br />
&nbsp; &nbsp; 熟悉相关事宜的一位美国律师不无偏激地向本报记者表示：“这样的政策让美国企业感到忍无可忍。中国出台自主创新产品认定标准等于是消灭了美企对中国改善经商环境的最后一线希望。”<br />
&nbsp; &nbsp; 面对美方的指责，中国商务部长陈德铭本月初表示，中国在政府采购中，认定本地产品的标准是根据其在中国境内实现的增加值，这对中国境内的所有企业，包括外资企业也是一视同仁的。<br />
&nbsp; &nbsp; 他强调，中国正在积极推进加入世贸组织《政府采购协议》的谈判，希望有关国家认识到中国作为发展中国家的国情，降低要价，为中国早日加入该协议创造条件。<br />
&nbsp; &nbsp; 国会敲打政府23日的听证会是一个月来，美国国会第二次就政府对华贸易政策展开检讨。上一次受审的官员是美国财长盖特纳。<br />
&nbsp; &nbsp; 在当天的听证会上，美国国会开始反思美国政府在中美贸易中的行政效率。<br />
&nbsp; &nbsp; “经过这些年，我们发现中国的情况已经变了，但美国对华工作方式却还是老样子，政府在对华经济工作上缺少一个拍板的人。”财政委员会主席博卡斯说。他进而表示，美国政府缺乏整体对华经济工作思路，工作职能被分散到多个部门之后缺乏统筹。<br />
&nbsp; &nbsp; 美国政府现在有三个部门负责美中经济事务：财政部、商务部、贸易代表办公室。尽管三方责任明确，但协调性不强。<br />
&nbsp; &nbsp; “作为一名美国老百姓，如果有军事问题，他可以问国防部，有外交问题，可以问国务院，但如果有贸易问题，他应该问谁？”博卡斯当天质问道。这让科克和骆家辉都愣了一下。然后科克才回答，美国政府是多部门协调处理贸易问题的。<br />
