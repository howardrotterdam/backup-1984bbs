昨天，棉棉本人没有到庭，她的代理人北京盈科律师事务所孙景伟律师说，去年，谷歌中国网站的“图书搜索”栏目收录了原告的《盐酸情人》，将作品扫描上网并供不特定访问者查看下载，这一行为并没有得到棉棉的许可。<br />
<br />
棉棉认为两被告的行为侵害了自己的著作权，请求法院判令两被告公司停止侵权，将作品从谷歌网站上删除，公开赔礼道歉，并赔偿经济损失、精神损害赔偿及维权费用共6.1万元。“索赔并不是主要目的。”孙律师告诉记者，棉棉这次打官司主要是为了讨个公道，赔偿数额可以商量，但谷歌必须承认自己有侵权行为。<br />
<br />
对此谷歌公司表示，涉案图片并不是谷歌公司扫描的，扫描件也没有保存在谷歌服务器。棉棉起诉后，他们已经在第一时间内断开了链接，已经尽到了作为搜索服务商的义务，因此不构成侵权。而且该代理人认为，他们扫描的书的片段只是为了让读者大致了解书的内容，起到一个简介的作用，标注出的只有400个字，而全书有13万字，网友根本无法通过扫描片段获知全书内容。<br />
<br />
此案未当庭宣判。<br />
