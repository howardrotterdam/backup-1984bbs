													<h2>电动车将划入机动车，上路需考驾照，上牌、买保险</h2>
						
						
																											<div id="postmessage_355067" class="t_msgfont">核心提示：<br />
<strong>国家标准管理委员会近日公布《电动摩托车和电动轻便摩托车安全要求》等标准。其中一项称“40公斤以上电动自行车划入机动车范畴”。这意味着，明年再骑电动车可能就要经过考驾照、上牌、买保险等手续。</strong>据悉，这项规定将影响2000多家电动自行车企业和1.2亿人口。<br />
<br />
千龙网12月3日报道 中华人民共和国国家标准《电动摩托车和电动轻便摩托车通用技术条件》(下称《电摩标准》)，将于2010年1月1日开始实施。这个标准的核心，是将40公斤以上、时速20公里以上的电动车，称之为轻便电动摩托车或电动摩托车，划入机动车范畴。<br />
<br />
这个区间的划定，等于是把刀直接架到了2000多家电动自行车企业的头上。<br />
<br />
“标准里面有一个具体的数字：40公斤的质量，而这个数字，就像是在我们家门口紧贴着门起了一堵墙，这堵墙一建起来，我们没有活路了。”清华大学教授马贵龙，这个被业内人士称为“中国电动车第一人”的学者对CBN记者表示。<br />
<br />
<strong>“低于40公斤的车做不出来”</strong><br />
<br />
在《电摩标准》的报批稿中，关于电动轻便摩托车的定义规定，最高设计车速大于20km/h且不大于50km/h，整车整备质量大于40kg且最高设计车速不大于50km/h。<br />
<br />
“我是搞技术的，现在最低配制的电动自行车，应该是36V、10Ah铅酸电池。按照这个最低配置，整车质量也将远远超过40公斤，现在用40公斤这个数字，把我卡住了，我车做不出来了。”马贵龙表示。<br />
<br />
“车架钢管的管壁不能低于1.2mm，否则不安全，必须用管壁为1.4mm的钢管。这样，单纯车架的重量就9公斤，电池的重量为17公斤，再加上零配件，低于40公斤的电动自行车做不出来。”爱马电动自行车董事长张剑对CBN表示。<br />
<br />
如果这项标准实施，将使2000多家企业失去希望，1.2亿电动自行车消费者的生活方式将会改变。<br />
<br />
从1995年清华大学研制的第一台轻型电动车出世开始，它独有的属性，节能、环保、便捷、价廉，使得它的市场日益扩大，电动自行车生产厂家增加到2000多家。2005年到2008年，全国电动自行车销量超过9000万辆。目前，我国拥有1.2亿辆电动自行车的市场保有量。<br />
<br />
有专家预测，轻型电动车产业一直保持80%以上的增长速度，产销量已经占到全球90%以上。到2010年，中国电动自行车产量将超过3000万辆，出口达500万~600万辆，并将逐步取代中国农村摩托车市场，向东南亚、南亚国家蔓延。<br />
<br />
10年前的标准<br />
<br />
然而，电动自行车的标准却是“10年一贯制”，没有变过。<br />
<br />
“电动自行车的标准是10年前的，时速低于20公里，已经不适合市场的需求了。在行业标准还没有出现的时候，电摩的标准出来了，把电动自行车的领域划为己有，如果电动自行车企业想生产质量大于40公斤、时速高于20公里的产品，必须申请电摩的批件，要求企业必须有8000万的注册资金，2个亿的固定资产，具备一定的检测能力，否则不能申请。事实上，电摩行业现在是限入，不接受新的申请。”张剑表示。<br />
<br />
而事实上，在这2000多家电动自行车企业中，能有这个经济实力的只有20多家，但是由于政策限制，即使有实力也不能从申请这条路上走过去，只能去找其他路子走。<br />
<br />
“我们只能去整合一些摩托车生产企业，只有这样，才能够继续生存下去。”张剑表示。<br />
<br />
《电摩标准》这把刀，把电动自行车的前路和末路，都给切断了，而相应的配套措施并没有出台。<br />
<br />
而执行了新标准之后，那些拥有电动自行车的人们，首先需要先去考驾照，然后去交管部门进行牌照申请，否则那些电动自行车将不能上路。<br />
<br />
作为《电摩标准》的起草人之人，全国汽车标准化技术委员会摩托车分标委会秘书长缪文泉在接受CBN记者采访时表示，《电摩标准》与电动自行车标准是两个完全不同的标准，很多人产生了误解。《电摩标准》是按照摩托车的制动安全、灯光要求等来制订的，也是根据国家的道路交通安全法来制定的。<br />
<br />
但事实上，电动自行车的时速标准在美国是32公里，欧洲25公里，日本是24公里，只有中国还是10年前的标准。<br />
<br />
“把电摩新国标与本来要修订的电动自行车标准‘无缝对接’，显然不合适。”马贵龙表示。<br />
<br />
以“安全”之名<br />
<br />
《电摩标准》的出台，是为了整顿交通安全，更好地管理电动摩托车。但是这个安全之名，在数据面前，似乎又显得无力。<br />
<br />
“在电动自行车事故中，95%是由于被撞，而不是电动自行车撞死人。”马贵龙表示。<br />
<br />
《电摩标准》在事实上把电动自行车划入机动车领域，反而增加了安全隐患。<br />
<br />
<br />
“准车质量大于40公斤，就是列为机动车了，车速大于20公里，也被列为机动车，从这两个最关键的技术数据来看，将有大量车速大于每小时20公里的电动轻便摩托车在机动车道上行驶，将大大增加车祸的发生率。如果在慢车道上将减少这种车祸发生率。”中国自行车协会电动车委员会主任陆金龙对CBN表示。<br />
<br />
建设部原副部长周干峙表示，对于交通的管理，关键是在车速上，城市限制摩托车是对的，关于电摩在交通管理上要考虑大家的安全，根据实际情况，进行分类控制，不能简单地不让它上慢车道，也不能让它上人行道。<br />
