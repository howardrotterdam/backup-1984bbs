													<h2>40公斤以上电动车将划入机动车 上路或需考驾照</h2>
						
						
																											<div id="postmessage_353815" class="t_msgfont">国家标准管理委员会近日公布的《电动摩托车和电动轻便摩托车安全要求》等4条关于电动摩托车和电动轻便摩托车的相关国家标准，将于2010年1月1日起实施。其中一项“20/40标准”备受瞩目――“40公斤以上、时速20公里以上的电动自行车，称为轻便电动摩托车或电动摩托车，划入机动车范畴”。这意味着，如果这一国标实施，明年再骑电动车可能就得经过考驾照、上牌、买保险等一系列手续了。<br />
国标：骑电动车要考驾照？<br />
新国标“20/40”标准把多数电动车划为机动车范畴，这意味着一批超过原标准的电动自行车将“升级”为摩托车，从慢行道骑上了机动车快行道。<br />
早在1999年，国内曾出台《电动自行车通用技术条件》，规定“最高时速不超过20公里，整车质量(重量)不大于40公斤”。据说，新国标是“承”这一文件而来。然而目前市场中几乎所有的电动自行车，都已超出该“20/40”标准。新国标继承老标准，却被指不符合目前的现实情况。<br />
记者与起草新国标的主要单位――国家机动车产品质量监督检验中心联系。该中心工作人员介绍，此次研究制定电摩新国标主要目的是为了规范电动车市场，保障电动车使用者和行人的人身安全。电摩新国标实施后，市场上销售的整车重量大于40kg、时速超过20km的电动两轮摩托车将划归到轻便摩托车中，而依据《机动车运行安全技术条件》中的相关规定，这类电动车可被纳入到机动车管理范围中。<br />
相应的，一旦轻便摩托车划归到机动车队伍，依据国家相关法规，驾驶人必须通过上牌照、考驾照、买保险等几道关才可以合法上路。<br />
自协将申请暂缓执行<br />
中国自行车协会助力车委员会主任委员陆金龙介绍，十年来，修订电动自行车技术标准的呼声越来越高，但国标迟迟未作修订，而市场中几乎所有的电动自行车，都已超出该标准。新出台的《电动摩托车和电动轻便摩托车通用技术条件》显示，市面上绝大多数电动自行车都将“自动”变身为轻便电动摩托车。初步统计，全国约达1.2亿辆。他表示，中国自行车协会将向国家相关职能部门提出申请，暂缓执行电动摩托车新国标。<br />
据了解，这一标准为推荐性标准。<br />
两千余家企业或退出市场<br />
据一家电动自行车公司的董事长倪建介绍，电动自行车超标，消费者、厂家心知肚明，“主要是总重量，也就是电池重量超标，消费者对行驶里程数的要求越来越高，提高行驶里程数，靠的就是电池”，他表示。不少消费者验车时装的老式电池，验完车上完牌，再换上新型电池。<br />
倪建说，目前，全国电动自行车生产企业达2394家，其中只有100多家系原来的摩托车生产企业。电摩国标实施后，由于重获电动摩托车和电动轻便摩托车生产许可，难度偏大，除该百余家摩托车厂家，其余两千多家企业都可能退出市场。<br />
超标电动自行车变摩托？市民有意见<br />
“慢”电动咋能上快车道<br />
每天骑电动自行车上下班的市民贾先生听闻电动摩托车新标准后就大为惊讶：不让超标电动自行车上路还好，如果非要我们去和机动车抢道，我宁愿不骑车。<br />
成都君士达律师事务所律师陈志伟认为，新标准是在电动自行车不断增多、管理难度增大的情况下出台的，从维护交通秩序的角度来看是适时的。但从此新标准内容上来看，是否征集了广泛意见、进行了相关听证明显有待商榷。如果按照这一标准实际操作，“电动摩托车”一旦上路，就会和公交车等机动车辆挤在一根道上行驶。近几年，因为非机动车侵走机动车道而发生的人员伤亡事故层出不穷，把顶着“摩托车”名号的超标电动自行车往机动车道上赶，不是更增加了交通事故发生的机率吗？这些“摩托车”的安全从何保障？陈律师进一步指出：任何政策、法规的制定出台都不能忽略市民的权益，否则其不但会饱受诟病，在具体实施时还会遭到广泛抵制，造成执行过程中“有法不依、执行不严”的现象。<br />
国标起草者之一：<br />
定位为不合格的机动车<br />
电摩国标起草者之一、全国汽车标准化技术委员会摩托车分标委会秘书长缪文泉接受记者采访时表示，超标电动自行车划归电动摩托车系误读。<br />
据缪文泉介绍，超标电动自行车即使重量、速度两项指标符合电动轻便摩托车的标准，即重量超过40公斤、速度超过20公里，也不能并入电动轻便摩托车范畴，“不管是电动摩托车，还是电动轻便摩托车，安全系统、制动系统，乃至于车灯，都有一套具体的技术标准。超标电动自行车不符合这些标准，所以不能算为电动摩托车”。<br />
虽然不能作为电动摩托车，但缪文泉坦言，“《道交法》有规定，非机动车时速不能超过20公里，目前绝大多数电动自行车都已超标，所以，也不是自行车”，他说，目前路面上、市场中的超标电动自行车，精确定义应为“不合格的机动车”。<br />
缪文泉表示，电摩确实将被划入机动车管理范围，驾照、交强险、车辆上牌、机动车司机需要遵守的所有规定，均适用于电摩驾驶者。但是，超标电动自行车是否应作为机动车管理，骑车人是否需要考驾照、买交强险，有待于国家相关部门的具体规定。<br />
（本文来自腾讯新闻）</div>

							
							
							
							
