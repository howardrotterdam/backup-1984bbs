中国国务院总理温家宝当地时间9月21日晚在纽约会见旅美华侨华人、中资机构及留学生代表时说，钓鱼岛是中国的神圣领土。日方最近在钓鱼岛海域抓扣中国渔民和渔船，至今不释放中方船长，这是完全非法的、无理的，已经对中方船长及其家人造成严重伤害，并激起海内外全体中国人的愤怒。中方多次严正交涉，日方置若罔闻，中国政府不得不采取必要的反制措施。在此，我强烈敦促日方立即无条件放人。如果日方一意孤行，中方将进一步采取行动，由此产生的一切严重后果，日方要承担全部责任。新华社记者 姚大伟摄<br />
