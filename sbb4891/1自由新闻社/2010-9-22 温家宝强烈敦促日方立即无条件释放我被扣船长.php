温家宝指出，经过多年艰苦努力，中日关系得到改善和发展，这一良好势头遭到严重破坏，完全是日方一手造成的。日本政府应该迅速改变错误做法，使中日关系回到正确轨道上来，这有利于两国人民的根本利益，也符合和平、合作世界潮流。</div>

							
							
							
							
