天安门毛画像会永保留<br />
<br />
无论对毛泽东有多少争议，但他是中华人民共和国的「国父」迨无疑义。即使在邓胡赵体制具体清算了毛的多少罪行，彻底否定了毛视之为其重大成就之一的「文化大革命」之后，亦未致全面「批毛」，邓小平甚至还说s「没有毛主席，至少我们中国人民还要在黑暗中摸索更长的时间。」近30年来的中共领导人，无论在具体做法上如何扬弃毛泽东思想，在理论上怎样修正毛泽东的观点，但在毛泽东和毛泽东思想方面，始终未敢全面否定，而是时而理直气壮、时而羞羞答答地打着这面大旗。<br />
<br />
