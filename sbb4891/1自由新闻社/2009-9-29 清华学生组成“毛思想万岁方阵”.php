【明报专讯】国庆巡游中新增「毛泽东思想」的方阵，高举「毛泽东思想万岁」的标语，这一消息昨日起在内地互联网上引起一阵热烈讨论，很多左派留言欢呼，认为是一个重要的转折点，也有自由派留言，担心这是胡温政权进一步向左转的信号。但看来，无论是左派还是右派，都过分看重了这一举动的象征意义。<br />
