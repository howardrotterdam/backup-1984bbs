													<h2>萧瀚：关于“六三六五”真相与和解的建议书</h2>
						
						
																											<div id="postmessage_43201" class="t_msgfont">关于“六三六五”真相与和解的建议书<br />
<br />
尊敬的胡锦涛主席、温家宝总理：<br />
<br />
你们好！<br />
<br />
由于深受国际形势和国内传统与现实的影响，近60年来，在国家制度方面，中国走上了一条现已被证明完全错误的道路，这个制度给人民造成了深灾巨创。<br />
<br />
无疑，这个制度的创建者及其执行者应对此负主要责任，但这并不意味着人民就是完全无辜的，任何一个制度的存在与运行并非仅仅只是掌权者的责任，不掌握政府权力的制度拥护者、合作者、沉默者，也是构成制度运行的要素――正如我本人也是生存于这个制度的罪人。<br />
<br />
德国战后全民大忏悔，南非种族和解，都是基于对每个人个体良知的关切，而成就其和解的最终结果。南非的真相与和解之精神与制度操作，对中国解决多年累积的怨怼，具有很高的示范价值。寻迹国史，中国数千年来累积仇恨、冤冤相报的历史模式，不但没有给中国带来福音，更是循环往复地制造灾难。<br />
<br />
为此，对于过往的制度性、集体性罪恶，需要清理而不是清算；需要真相，也需要和解；没有真相就没有和解，没有和解精神，真相只是下一轮罪恶与灾难的起点。如果改变原有的史辙，摒弃积怨与清算模式，而代之以真相与和解的精神，可能是政府和人民的共同出路。毕竟，人口如此庞大的中国，长期累积仇恨将是一件令人恐惧的事情，中国历代王朝覆灭之际灭绝性的人口减少便是千年史鉴。<br />
<br />
1989年的春夏之交，由于各种复杂的国内国际原因，北京发生了一场令后人扼腕揪心的悲剧――“六三六五”天安门事件。虽然这仅是诸多制度性灾难之一，但这场悲剧迄今还是中华民族很深的伤口，它还在继续化脓、流血，如果不做好有效的和解与善后工作，在复杂的国内国际政治、经济、社会、文化等综合因素的刺激下，这伤口会不会成为更大悲剧的诱因，实难逆料。<br />
<br />
今年六月四日，是“六三六五”天安门事件的20周年忌日，不知中共中央和国务院是否考虑圆满妥善解决此事，为此，谨提出以下参考性建议：<br />
<br />
一． 全国人大和政协两会期间，将“六三六五”和解作为一个重要议题提交两会讨论；<br />
<br />
二． 在全国人大成立由一定人数（例如10到20人）组成的“六三六五和解委员会”，由中国共产党和国家重要领导人担任委员会主席，邀请部分前国家领导人担任委员，并由具有社会公信力的部分官员、人大代表、政协委员、社会活动家、其他公民加盟委员会，参与委员会的工作；<br />
<br />
三．“六三六五和解委员会”的工作内容包括但不限于以下：1.全面公布“六三六五”真相，公开政府解决“六三六五”和解问题的方案，为此创办新的媒体，例如报纸、杂志、网站、电台、电视台等；2.公布罹难者名单（包括不幸死难的官兵在内）以及因此事而后续受到不公平待遇的中国公民名单，并且向他们以及家属公开致以国家道歉，向全国人民道歉；3.为包括在此事件中死难的解放军官兵在内的所有“六三六五”死难者下半旗致哀；4.主动按照上述第二项的名单发放国家赔偿金；5.允许民间在有序前提下自由开展纪念“六三六五”活动；6.允许任何成年公民向委员会提供新的事实、提交自己的意见建议、表达异议；7.在国家赔偿之外，允许罹难者家属以及受迫害者本人及其家属向委员会提出更高的国家赔偿要求，由委员会根据具体情形裁断；8.恢复因“六三六五”原因而流落在外的中国公民自由返国的权利。……<br />
<br />
也许在处理问题过程中会出现一些问题，但是只要完善可操作性的方案，认真细致地做好工作，一些可能的隐患与动荡当会消除，只要坚定地秉持真相与和解精神，哪怕发生一些突发事件，也会得到有效的和平解决。<br />
<br />
另外，今年也是法轮功问题十周年，若以上述类似思路和方法来消除怨怼，或许能使我们国家在此问题上的政治、社会矛盾得到有效疏泄。<br />
<br />
我相信，如果能够开启“六三六五”事件的真相与和解行动，或许能够成为进一步解决其他制度性新仇旧恨的起点，从而为中国最终走向真正健康与和平、繁荣与稳定的未来打开大门。<br />
<br />
尊敬的胡主席、温总理，上述是我作为一个公民的主张，也许微不足道，但国家是每一个人的国家，为了我自己的良心安宁，也为了中国每个人的未来之幸福与和平安宁，我谨公开提出上述建议，供你们参考，也供其他所有中国公民参考与讨论。<br />
