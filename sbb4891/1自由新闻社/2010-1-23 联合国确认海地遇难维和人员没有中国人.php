Press Conference by Heads of Peacekeeping Operations,<br />
Field Support Departments on Situation in Haiti<br />
The earthquake in Haiti had so far resulted in 14 fatalities among United Nations personnel and 56 staff members injured, but reports that the Secretary-General’s Special Representative was among the casualties could not be confirmed, the Organization’s peacekeeping chief said at a Headquarters press conference today.<br />
Alain Le Roy, Under-Secretary-General for Peacekeeping Operations, confirmed that Hédi Annabi, Special Representative and Head of the United Nations Stabilization Mission in Haiti (MINUSTAH), had been in his Christopher Hotel headquarters building in Port-au-Prince at the time of the earthquake.&nbsp;&nbsp;The Secretary-General had decided to send Edmond Mulet, Assistant Secretary-General for Peacekeeping Operations, to serve as Acting Head of Mission as of Friday.<br />
Accompanied by Susana Malcorra, Under-Secretary-General for Field Support, Mr. Le Roy said some 3,000 Mission troops and police were currently in the Haitian capital to secure the airport, patrol the city and dig for survivors.&nbsp;&nbsp;MINUSTAH headquarters had collapsed, as had the Montana Hotel and another facility in which United Nations staff lived.&nbsp;&nbsp;The airport near Port-au-Prince was operational, but its control tower had collapsed.&nbsp;&nbsp;The two major needs now were search-and-rescue teams with heavy equipment and medical teams, he said.<br />
Ms. Malcorra added that there was also a need for water and rations.&nbsp;&nbsp;United Nations staff members were already sharing their rations with the population, and MINUSTAH’s logistics base was now serving as its headquarters and assisting with medical support.&nbsp;&nbsp;The Mission was assessing the situation and providing support to Haitians.<br />
She said the names of the 14 fatalities had been cross-checked with troop-and police-contributing countries.&nbsp;&nbsp;Among them were three Jordanian and 10 Brazilian military personnel, as well as a Haitian civilian.&nbsp;&nbsp;Although Mission engineers were on the ground, they lacked the expertise and tools to handle destruction of such magnitude and were therefore moving with caution in order not to exacerbate the situation.&nbsp;&nbsp;The airport was operational but control activities had to be executed manually, she said, adding that the United Nations was pre-positioning a plane that could start shuttling between Miami and Port-au-Prince on Friday.<br />
Answering questions, she said the only confirmed United Nations civilian fatality was a Haitian, but she feared there would be many more.&nbsp;&nbsp;While United Nations staffers were also among the injured, the confirmation process was a delicate one as names had to be cross-checked through duplicate records since the original records had been in the destroyed headquarters building.<br />
Next-of-kin must be informed before media, and the process had to be done in the right way so as to prevent negative or positive expectations among family members, she emphasized, adding that she had already had several difficult conversations with relatives of United Nations staff, during which she had only been able to say that around 150 staffers had not been accounted for.<br />
Mr. Le Roy added that 10 people had been extracted alive from the rubble of the Christopher Hotel during the day, and that people were still being rescued.&nbsp;&nbsp;He had no figures about the other facilities as communications between the sites were not possible and information received by the logistics base was sketchy.&nbsp;&nbsp;The rescued people had all come from the lower floors of the headquarters building.&nbsp;&nbsp;The top floors, where Mr. Annabi had probably been when the earthquake struck, had been the most affected.<br />
Responding to questions about the Mission’s future, Mr. Le Roy said it had achieved a lot and was still needed in Haiti, especially with regard to the upcoming elections in October.&nbsp;&nbsp;The Force Commander had been out of the country during the earthquake and was on his way back.&nbsp;&nbsp;Until his return, the Deputy Force Commander was in charge, and tomorrow, Mr. Mulet would take over command of the Mission’s civilian component.<br />
Asked about the equipment required, Ms. Malcorra said general equipment was available but that there was a need for sensors to listen for signs of life under the rubble.&nbsp;&nbsp;There was also a need for expertise and guidance.&nbsp;&nbsp;Two rescue teams from the United States and a Chinese one were already at work while another from the Dominican Republic was approaching the site by road.&nbsp;&nbsp;Every minute counted, she stressed, adding that priorities must be managed as there was limited capacity to transport supplies from the airport to the sites.<br />
On the subject of emotional consequences among United Nations staff, she said the situation was difficult for all, but the dedicated peacekeeping personnel were focused on what had to be done.&nbsp;&nbsp;Telephone lines had been set up to answer calls from family members, and counselling services were available for staff on the ground.&nbsp;&nbsp;Additional trauma counsellors were on the way, she added.<br />
Mr. Le Roy said emotions were running high and although no figures were available, the number of casualties might be the highest in United Nations history.<br />
Asked who was coordinating rescue efforts, he said President René Préval had visited the logistics base, adding that Mr. Mulet’s first task upon arrival would be to coordinate with the Haitian authorities.&nbsp;&nbsp;The Secretary-General was briefing the General Assembly and would coordinate appeals for assistance.&nbsp;&nbsp;The Office for the Coordination of Humanitarian Affairs (OCHA) would coordinate a flash appeal, probably on Friday.&nbsp;&nbsp;MINUSTAH had to focus simultaneously on rescue operations and helping the Haitian population.&nbsp;&nbsp;Control of the airport was currently in the hands of the United Nations, but the United States would probably take over air operations.<br />
In reply to other questions, Ms. Malcorra said Port-au-Prince was not a family duty station.&nbsp;&nbsp;The Christopher Hotel had been inspected for compliance with minimum operating security standards and some adjustments had been made.&nbsp;&nbsp;MINUSTAH’s budget was sufficient to accommodate immediate needs, such as construction of temporary housing, she added.<br />
