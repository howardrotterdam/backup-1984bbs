																											<div id="postmessage_704788" class="t_msgfont">志愿军老兵欲重回朝鲜 月收入300无法成行 <br />
2010年06月26日 11:05 大洋网-广州日报 <br />
<a href="http://news.ifeng.com/history/zhongguoxiandaishi/detail_2010_06/26/1675326_0.shtml" target="_blank">http://news.ifeng.com/history/zh ... /26/1675326_0.shtml</a><br />
核心提示：前不久，赴朝旅游专列开通后，他们的这种愿望愈加强烈。但去朝鲜又是件可望而不可及的事情，“去不了啊……说白了就是没有钱！”一位老兵叹着气说。这些老战士们，除了享受每月由国家民政部门发给的300多元在乡复员军人定补外，再无其他收入，凭自身的力量，这些老迈的身躯根本难以走出家门……<br />
<br />
<br />
<br />
邱树林、王凤和等老兵在一起回忆过去，回忆曾经的光荣与骄傲。来源：广州日报<br />
<br />
<br />
<br />
“我叫王凤和，今年81岁，原中国人民志愿军第40军120师358团高射机枪连射手兼班长……” 来源：广州日报<br />
<br />
<br />
<br />
“我叫李树国 ，男 ，今年77岁，原中国人民志愿军第40军118师353团高射炮机枪连射手，1950年10月28日参加抗美援朝作战……” 来源：广州日报<br />
<br />
我们想回朝鲜看看！<br />
<br />
夕阳下的邱树林与马常清，想去朝鲜看看是这些老兵最强烈的心愿。<br />
<br />
吉林省农安县“凤英荣誉军人院”内的抗美援朝老兵：<br />
<br />
“我们想回到朝鲜看看！看看当年战斗的阵地！看看烈士陵园！看看永远埋葬在异国的战友……”<br />
<br />
朝鲜战争<br />
<br />
60年前的今天（1950年6月25日），朝鲜战争爆发。6月27日，美国总统杜鲁门发表申明，在扩大朝鲜战争的同时，命令美太平洋第七舰队开进台湾海峡，粗暴干涉中国内政。9月15日，美军在仁川登陆，随后越过三八线，将战火烧到鸭绿江边。<br />
<br />
当年，美国的钢产量是870万吨，中国只有61万吨，美军一个师有959门火炮，140多辆坦克，3800辆车辆，志愿军一个军只有522门火炮，临时配有100辆车辆，没有坦克，更没有制海权与制空权……<br />
<br />
但不足周岁的共和国下了最大的决心，在1950年10月19日夜晚，中国人民志愿军4个军和3个炮兵师共26万人，在没有空军掩护的条件下，分别从安东（今丹东）等地，雄赳赳，气昂昂，跨过鸭绿江，入朝参战……<br />
<br />
我先后入朝参战的部队达290万人，经过两年零九个月的英勇激战，付出巨大伤亡的代价，中朝军队迫使美国在停战协定上签字，将军事分界线重新推回到三八线，美军总司令克拉克将军获得了“一项不值得羡慕的荣誉”，成为“历史上签订没有胜利的停战条约的第一位美国陆军司令官。”<br />
<br />
而对这场战争的结果，正如彭德怀所说：“它雄辩地证明：西方侵略者几百年来只要在东方一个海岸上架起几尊大炮就霸占一个国家的时代是一去不复返了！”此后，新中国由此赢得了一个长时间相对稳定的和平环境。<br />
<br />
据不完全统计，抗美援朝期间，仅东北地区就动员了近40万人参军……就在广州，也出现了大、中学生入伍的高潮，据记载，“两万两千多人报名参加军干校，被批准录取的有3338人，分别输送到陆军、空军、通信兵、铁道兵等兵种……”<br />
<br />
在战火纷飞的朝鲜战场上，13.3万优秀中华儿女，为打击侵略者、保家卫国，献出了宝贵的生命。在这场异常残酷的战争中，中国人民志愿军谱写了气吞山河的英雄壮歌，创造了人类战争史上以弱胜强的光辉典范。<br />
<br />
在朝鲜战争爆发60周年之际，本报记者来到抗美援朝老兵较为集中的东北地区吉林省农安县，找到几位曾参加抗美援朝第一战的老兵，聆听那历史上不平凡的那一篇章……<br />
<br />
（相关史实参考金冲及《二十世纪中国史纲》第三卷）<br />
<br />
本报东北特稿/记者杜江摄影报道<br />
“我叫刘清，今年78岁，原中国人民志愿军第27军某部尖刀连战士……在参加第四次战役一次穿插任务后，尖刀连就剩下了俩……” 来源：广州日报<br />
<br />
<br />
<br />
“我叫邱树林，81岁，中国人民志愿军第27军特务团警卫员。1950年10月25日入朝作战，在第二次战役东线作战中，打垮了美王牌军‘陆战一师’……”来源：广州日报<br />
<br />
60年前誓将纸虎化为尘<br />
<br />
60年后不灭光辉不朽身<br />
<br />
黄继光邱少云……122位英烈长眠在这里<br />
<br />
抗美援朝烈士陵园位于沈阳市北陵公园东侧，占地 24万平方米，1951年开建。黄继光、邱少云、杨根思、孙占元等122名烈士长眠于此。<br />
<br />
陵园正中耸立着一座雄伟的英雄纪念碑，正面镌刻董必武题词“抗美援朝烈士英灵永垂不朽”；背面刻有郭沫若的题诗：“煌煌烈士尽功臣，不灭光辉不朽身。鸭绿江南花胜锦，北陵园畔草成茵。英雄气魄垂千古，国际精神唤万民。峻极高山齐仰止，誓将纸虎化为尘。”<br />
<br />
碑的顶部是中朝两国国旗，寓意着两国人民用鲜血和生命染红的战旗永远高高飘扬。旗下是手握冲锋枪的志愿军战士铜像，眉宇间流露出一种凛然不屈的英雄气概。<br />
<br />
下岗女工办的“拥军大院”<br />
<br />
吉林省长春市农安县郊一栋被称为“拥军大院”的平房里，聚集着一群装容奇特的老人：他们穿着新旧不一、年代各异、样式不齐的绿军装，整齐地佩戴着红领章与五星帽徽，有人胸前还挂着各式军功章――这是一群抗美援朝老兵！略知战史的人都知道，27军、40军均是当年在朝鲜战场上威名赫赫的“英雄部队”。<br />
<br />
对于远道而来的记者，老兵们回忆起当年在朝鲜战场上烽火连天的战斗岁月，其中，被称为“王班长”的王凤和经历最具代表性，他所在的40军是最早入朝参战的队伍之一。<br />
<br />
“雄赳赳，气昂昂，跨过鸭绿江……”据王凤和回忆，60年前，40军120师358团在安东（今丹东）过江，他清楚地记得鸭绿江大桥的长度是1100米。当部队走到桥中心，也就是两国分界线时，连长看了看手表对大家说：同志们！请记住吧，现在是1950年10月22日晚8点30分，这一步迈过去就是朝鲜的土地了！听完连长的话，一种真正走上战场的心情犹如江水涌来，每个人都回头流着眼泪向祖国告别……<br />
<br />
进入朝鲜的第一幕让王凤和记忆犹新：“……一名朝鲜妇女躺在血泊里，手牵的黄牛也被美国飞机炸死，但她背上小女孩仍活着，白色的衣服上满是鲜血和泪水，孩子哭着叫着：‘阿妈妮！阿妈妮！……’这哭声声声打在我们的心坎上，大家的心都要被小女孩哭碎了……排长从死者身上解下女孩，由战士们轮流背着前进，此时，敌机轰炸扫射不断，每走一步都很艰难，直至遇到一群向北疏散转移的朝鲜老乡时，我们才将孩子交给了其中一名妇女……“<br />
<br />
3天后，王凤和所在的40军打响了抗美援朝第一枪。首战在温井伏击战中，歼灭南朝鲜军一个营和一个炮兵中队，击溃一个团，在随后云山之战中，中国军队歼灭美骑兵第一师第八团，创造了首次以劣势装备打击美军的成功战例。1950年10月25日，也被认定为抗美援朝战争纪念日。<br />
<br />
在朝鲜战场，王凤和还经历了艰苦程度不逊于上甘岭的马良山防御战，他回忆到：“当时后方敌机炮火封锁，东西运不上来，战士们几天吃不上一口饭，喝不上一口水，嘴唇干裂渗出了血，喉咙渴的要冒烟……一边吃牙膏、啃树根，一边喝自己的尿坚守阵地……有一天半夜，炊事员冒着敌机轰炸与炮火封锁、送上来一包土豆、半包萝卜，尽管每个人都极度饥饿干渴，但没有一个人去动这些东西……最后，连长和炊事员亲自动手，先将东西分给伤员，而后每个战士分到一个土豆、半个萝卜，可没等我们吃完，敌人又开始进攻了……四五十架敌机上下翻飞，炮弹像雨点一样袭来，整个阵地弹片呼啸，硝烟弥漫，一片火海……机枪扫射与手榴弹的爆炸声响成一片……山上的树木已烧成了灰，山头不知道翻了几遍，黄土已变成了黑土，随便抓一把就有好几块弹片……我们仍然守住了山头……”<br />
<br />
“战斗停止了，敌人的尸体遍布整个山头，我们也有很多战友牺牲。大家低着头，流着泪，向牺牲战友的遗体告别：我亲爱的战友们，你们为抗美援朝、保家卫国而英勇献身！安息吧！亲爱的战友，祖国和人民不会忘记你们，我们这些活着的战友永远不会忘记你们的……”<br />
<br />
提起牺牲的战友，老兵们老泪纵横，大家也提出了一个共同的心愿： “我们想回到朝鲜看看！看看当年战斗的阵地！看看烈士陵园！看看永远埋葬在异国的战友……”“想看看朝鲜建设得如何了？”有生之年到朝鲜走走，是每位老兵最强烈的念头。<br />
<br />
前不久，赴朝旅游专列开通后，他们的这种愿望愈加强烈。但去朝鲜又是件可望而不可及的事情，“去不了啊……说白了就是没有钱！”一位老兵叹着气说。这些老战士们，除了享受每月由国家民政部门发给的300多元在乡复员军人定补外，再无其他收入，凭自身的力量，这些老迈的身躯根本难以走出家门……<br />
<br />
老兵们所在的这个名为“拥军大院”或“凤英荣誉军人院”的机构，是由一位叫张凤英的下岗女工自己掏腰包开办的，刘清、邱树林、马常清等3位老兵长期住在此处，另有十几位老兵常来此活动……这座“拥军大院” 条件是简陋的。用老兵马常清的话来说：“没有像样的宿舍，没有像样的食堂，没有像样的仓库……”此外，并不富裕的张凤英还收留了十几个智障人……<br />
<br />
这位有爱心的东北女人为了几十口人终日辛苦地劳碌着……操持着……“凤英荣誉军人院”――这个位于东北广袤大地上的小小院落，是老兵们重要的一个依托，这小院聚集和收藏了他们――曾经的中国人民志愿军战士：60年前的记忆， 60年前的荣光，60年来的骄傲。</div>

							
							
							
							
