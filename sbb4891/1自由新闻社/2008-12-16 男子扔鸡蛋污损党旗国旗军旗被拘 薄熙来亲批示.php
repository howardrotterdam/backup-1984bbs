													<h2>男子扔鸡蛋污损党旗国旗军旗被拘 薄熙来亲批示</h2>
						
						
																											<div id="postmessage_10622" class="t_msgfont">　　<a href="http://news.163.com/08/1216/11/4T9H0CMA00011229.html" target="_blank">http://news.163.com/08/1216/11/4T9H0CMA00011229.html</a> <br />
　　 <img src="http://img2.cache.netease.com/cnews/2008/12/16/200812161110274f7d6.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　红岩魂广场 资料图<br />
<br />
　　华商网-重庆时报12月16日报道 近日，重庆市公安机关经过近10天的艰苦奋战，成功侦破沙坪坝区红岩魂广场污损旗帜案，忠诚捍卫了党和国家的形象和尊严。中央政治局委员、市委书记薄熙来和市长王鸿举对此专门作出批示，充分肯定了重庆市公安机关所作的努力，认为此案的迅速侦破显示了公安机关的政治、业务素质。 <br />
　　 <br />
　　党旗、国旗、军旗等被污损 <br />
　　 <br />
　　11月23日晚7时40分，沙坪坝区烈士陵园红岩魂广场梯道中间的党旗、国旗、军旗、团旗、队旗五面旗帜被人用黑色黏稠液体投掷污损。 <br />
　　 <br />
　　这一严重政治案件发生在红色革命圣地歌乐山下，且作案时间临近“11.27”大屠杀纪念日，案件性质十分恶劣，在当地群众中激起了极大愤慨，大家纷纷表示“这里是革命烈士的安息之处，我们每个人都视这里为最神圣的地方，想不到竟有人胆敢如此嚣张！” <br />
　　 <br />
　　此案也引起了中央和市委领导的高度关注。中央政治局委员、市委书记薄熙来和市委常委、政法委书记刘光磊等领导同志作出批示，要求尽快组织侦破，消除影响。 <br />
　　 <br />
　　出警105人次 排查873人 <br />
　　 <br />
　　市公安局立即组织沙坪坝区公安分局、市局指挥中心、刑警总队、国保总队、文保分局、行动技术总队、网监总队等单位精干警力组成专案组，迅速投入案侦工作。由于此案嫌疑人作案时天色已晚，作案地点较为僻静，且案发地点周边人流量大，案侦线索缺乏，侦破难度很大。 <br />
　　 <br />
　　11月26日，市局常务副局长王立军召集所有参战单位召开专案工作会，严肃指出：此案是对党和国家形象的损害，是对民主法制的破坏。他要求公安机关加强对社会矛盾点和单位内部矛盾点的排查，务必快速侦破。 <br />
　　 <br />
　　随即，各参战单位出动警力105人次，对873名重点人员进行了排查。通过大量艰苦摸排，专案组排除了50名重点工作对象作案的可能性，得到反馈信息100多条，并根据其中一条重要线索出击，于12月1日下午在高新区将犯罪嫌疑人张敬之抓获归案。 <br />
　　 <br />
　　两名犯罪嫌疑人被刑拘 <br />
　　 <br />
　　经审讯，张敬之交代了制作油漆鸡蛋并到现场投掷污损旗帜的作案过程。经警方多方查证和心理攻坚，张在被抓26个小时后又交代出了另一同案嫌疑人，但只知姓文。专案民警通过对大量人口资料的搜索、摸排和甄别，终于发现一名叫文廷玉的人与张所供述的嫌疑人特征十分吻合，经张敬之辨认照片，文廷玉正是其作案同伙。 <br />
　　 <br />
　　12月2日，专案民警在沙坪坝区向乐村将文廷玉抓获。据嫌犯交代，案发当晚，由张准备好作案工具后，张、文二人相约窜至红岩魂广场，分别朝五面红旗各投掷了4枚油漆鸡蛋。至此，此案全案告破。 <br />
　　 <br />
　　目前，根据刑法第299条之规定，公安机关已依法对两名犯罪嫌疑人实施刑事拘留。 <br />
　　 <br />
　　公众场合故意毁损 <br />
　　 <br />
　　可处三年以下徒刑 <br />
　　 <br />
　　《刑法》第二百九十九条规定：在公众场合故意以焚烧、毁损、涂划、玷污、践踏等方式侮辱中华人民共和国国旗、国徽的，处三年以下有期徒刑、拘役、管制或者剥夺政治。 <br />
　　 　　《国旗法》第十九条规定：在公众场合故意以焚烧、毁损、涂划、玷污、践踏等方式侮辱中华人民共和国国旗的，依法追究刑事责任；情节较轻的，参照治安管理处罚条例的处罚规定，由公安机关处以十五日以下拘留。 (本文来源：华商网-重庆时报 ) <br />
