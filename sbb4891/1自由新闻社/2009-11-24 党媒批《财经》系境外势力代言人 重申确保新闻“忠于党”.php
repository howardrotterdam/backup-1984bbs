													<h2>党媒批《财经》系境外势力代言人 重申确保新闻“忠于党”</h2>
						
						
																											<div id="postmessage_339582" class="t_msgfont">《财经》不能不说<br />
<a href="http://energy.cctv.com/20091119/101887.shtml" target="_blank">http://energy.cctv.com/20091119/101887.shtml</a><br />
 <br />
CCTV.com&nbsp;&nbsp;2009年11月19日 10:06&nbsp;&nbsp;进入复兴论坛&nbsp;&nbsp;来源：&nbsp;&nbsp;<br />
据新华社《t望》杂志报道：<strong>境外利益集团利用在华获得的丰厚利润，自己培育并拥有分析师，或高薪聘请优秀华人学者担当顾问或独立董事，通过境内外媒体精心包装、刻意打造，提供各类活动舞台，提高其知名度、美誉度，从而成为中国国内行业精英，拥有强大的话语权，以其影响行业乃至国家宏观经济决策。</strong><br />
 <br />
文章所指并非危言耸听。随着一些媒体“得天独厚”的资金实力、传播手段等优势，在极短的时间内制造了一连串轰动的“黑幕”、炮制了一系列所谓独立经济学家，在中国经济的决策面颐指气使，误导国内舆论，严重干扰了中国市场经济的正常运行。<br />
 <br />
《财经》杂志揭黑性报道的实质<br />
 <br />
“中国财经记者第一人”胡舒立创办的《财经》，在创办开始的几年内屡有斩获，以其全景式的报道方式透析“琼民源事件”，披露“基金黑幕”，揭发“银广夏”，掀开“庄家吕梁”、“亿安科技”、“蓝日”、“德隆”等一系列黑幕，震惊全国。<br />
 <br />
现在回转头来看《财经》的揭黑报道，其对中国证券市场造成的影响可谓深远。<br />
 <br />
1998年“君安震荡”，将中国百姓对券商的崇拜打垮；2000年“基金黑幕”将中国百姓对公募基金的信赖击碎；2001年“庄家吕梁”将中国百姓对私募基金的痛恨煽动到极点；而接下来01年6月，正是A股2245点的时候，“亿安科技”出台，A股信心开始涣散，掉头向下；到8月A股稍微反弹一下，“银广夏”宜将乘勇追穷寇，中国百姓对上市公司的信心完全崩溃。此后，贱卖银行1万亿，贱卖其他国产至少还有1万亿的五年慢慢大熊市，就此开始。<br />
 <br />
然而，《财经》关键时刻的揭黑报道对于外资来说是个大利好。来看一个事实：2001年11月10日，中国加入WTO，此后外资金融机构叩开国门，五年大熊市，此时的上市公司成为“舞弊”和“诈骗”的代名词时，外资一进来就得到了一个非常便宜的好价格。<br />
 <br />
再反过来思考，假如当时的揭黑报道是偶然所谓，《财经》掀起的“揭黑风暴”，为什么这几年却不见踪影了呢？为什么在外资幸福地进入中国之后，就渐趋沉静了呢？<br />
 <br />
并且，与对待中国企业不同，面对外资金融机构在中国的种种弊行，胡舒立和《财经》企业却又变得大度和宽容了，譬如瑞银承销中石油卑鄙肮脏，《财经》不谴责、不判断；更有对高盛高华这样的国际金融资本绕开中国监管壁垒，设立假合资券商的违法行为，2004年8月16日《财经》杂志竟然以“高盛进入中国证券业突破即将来临”为题，称颂这一违法行为为“酝酿近三年而操作一年有余的精巧运筹，终于导出一个引入外资市场化处置证券公司风险的创举！”<br />
 <br />
所以，当三月份股市暴跌复暴跌的时候，以“独立、独家、独到”自诩的胡舒立及《财经》杂志，联合谢国忠、许小年对亿万百姓喊出“不应救、不能救、亦不必救”时，或许就一点也不奇怪了。<br />
 <br />
所以，当“谢国忠们”唱空中国楼市、股市的时候就不能奇怪《财经》照样在显著位置，用特别的标题，将谢国忠奉为“《财经》特约经济学家”，连篇累牍的让他抒发“泡沫中国”“崩溃中国”的论调了。<br />
 <br />
胡舒立其人其事<br />
 <br />
这是一份中所周知的建立：1982年胡舒立从中国人民大学毕业，到《工人日报》当记者；1985年，美国已将民间机构---世界新闻研究所邀请她，到美国进行为期5个月的访问；1987年的8月胡舒立拒绝了在洛杉矶一家报纸的工作机会，回国。<br />
 <br />
美国的“民间机构”星罗棋布，但是大多数背后都有特定倾向的财阀支持。“世界新闻研究所”全称叫word press institute,简称WPI，至今已经有48年历史，到2008年一共邀请过94个国家共500人到美国“访问”，中国至今除胡舒立外，还有一个叫吴昌华的女士1993年“受邀访问”过，吴昌华现任由汇丰银行等40多加国际“民间组织”发起的“非政府机构”世界气候机关中国区的总裁。<br />
 <br />
按照这个“民间机构”的受邀标准，申请者必须提交五方面材料来证明自己具有潜在的领导能力：1.五年以上全职新闻工作经历；2.流利的英语；3.几篇发表的文章；4.三封推荐函；5.三个以上的工作范本。<br />
 <br />
明明是新闻研究所，却要受邀者“具有潜在的领导能力”，是培养新闻记者呢？还是培养政治领袖？第二点奇怪的是，其时胡舒立才从大学毕业参加工作才三年，不符合“五年以上全职”的条件，难道美国人对此视而不见？<br />
 <br />
胡舒立在美国一直待到1987年8月才回国，其间一年多的时间在什么地方？干什么？不得而知。<br />
 <br />
此后胡舒立1992年从认知10年的《工人日报》到众多第一家民营报纸《中华工商时报》任编辑部主任；1993年，胡舒立赴美国华盛顿接受外国记者中心（COFJ）的专业培训；1994年胡舒立赴美国斯坦福大学作为期一年的学习，研读发展经济学；1995年他获得COFJ颁发的“杰出新闻记者奖”；1998年《财经》创刊，胡舒立任主编；2001年，被美国《商业周刊》评选为50位“亚洲之星”之一；2004年美国世界HR实验室评价她为“中国最具价值的财经记者”。<br />
蹊跷的是，1995年COFJ颁发给胡舒立“杰出新闻记者奖”，此时《财经》还没有创刊，基金、银广厦黑幕等还没被揭露，胡舒立个人离开中国新闻岗位已经2年多了，是什么报道能让胡舒立担当“杰出新闻记者奖”的荣誉呢？<br />
 <br />
不过，1998年胡舒立创办的《财经》杂志。既有新闻专业素养，又有发展经济学专长，还有“国际声望”，自认总编，就水到渠成、实至名归了。<br />
 <br />
看这份公开的胡舒立简历会发现，一个中国人用中文写作，在中国媒体工作，报道中国人的事情，也是给国人看的，但是美国人却用“访问”、“培训”、“进修”等孜孜不倦的给予培养，在她还没有“杰出”的时候，就提前预支给她“杰出新闻记者将”；在《财经》初出茅庐的第三年，美国人就从亚洲30亿人中，将她选拔出来，评她为50位“亚洲之星”之一，让她和全亚洲当年最优秀的政治领袖、科技精英平起平坐。<br />
 <br />
至于美国人的那个“HR实验室”的“中国最具价值的财经记者”，胡适立有过什么价值？最具有什么价值？对谁最有价值？则似乎欠缺一点解释。<br />
 <br />
总之被包装成了名记之后，胡舒立就披上了一件光彩的外衣。中国政法大学刘纪鹏教授不满意《财经》和许小年唱空中国股市，写了一篇“《财经》杂志悠着点儿”的文章，事后先把这篇文章寄给了胡舒立主编，希望能够在《财经》杂志上发表，但被婉言谢绝。之后刘教授听说《21世纪经济报道》有魄力，准备删减后稿子在那儿发表，结果也无果。<br />
 <br />
事后刘教授才听说，“《财经》杂志在北大办了基金，奖励最优秀的财经记者，财经记者要想获得那个奖，那当然不能得罪《财经》杂志。”刘教授在博客中感叹：“令人尊敬的互助便已是中国财经界的女杰，除了我这样一把年纪也没什么发展前途的人，谁又得罪得起呢？”<br />
 <br />
《财经》的资金哪里来？<br />
 <br />
凭借胡舒立《财经》之前乏善可陈的从业经历，如何足以使她刚刚切入陌生的证券行业，就能够步骤鲜明、点穴掏心的完成《财经》一举成名的五大杰作呢？是不是有资本市场阅历丰富的资深人士，对《财经》的工作给予了精心的规划？<br />
 <br />
另一方面对于基金黑幕、银广厦等问题的长期深入调查，恐怕不是当时名不见经传的《财经》能实现的，必须部署广泛的证券人脉关系，设计科学先进的调查方案和手段，而此前一直有利于证券行业的胡舒立，恐怕也是无能为力的。<br />
 <br />
再者，《财经》1999年在北京大学中国经济研究中心设立《财经》奖学金，后来由创办北大财经新闻研究中心，胡舒立自任主任，解析来又与英国《金融时报》共同举办财经新闻国际培训项目。<br />
 <br />
《财经》的钱从哪里来？<br />
 <br />
2004年10月24日《中国经济时报》刊登了记者单羽清写的一篇文章《胡舒立：生而<br />
 <br />
 <br />
不有，为而不恃》，文中胡舒立说在《财经》1岁的时候，发行量才7000份，而且绝不搞有偿新闻、关系稿编辑部的采访费用还充裕，自负盈亏的杂志，发行区区7000份，常识推断，此时《财经》应该是捉襟见肘的经济状态，但是它不但“采访费充裕”而且能到北京大学设立奖学金。<br />
 <br />
同样这篇文章透露了一个信息《财经》编辑部里“埋首工作的人中有金发碧眼的外籍人士”一个内宣刊物，也需要外频翻译？难道《财经》是中外合资媒体？<br />
 <br />
事实上，90年代中后期确实已经有少数外资投行进入了中国。民间私密调查队当时的中国企事业，仅仅是西方旃檀电影里虚构的情节，但是对西方大公司，却是家常便饭。难道是他们提供了帮助？<br />
 <br />
据财经内部人士透露，《财经》杂志的收益主要来自三部分：广告、发行和举办会议、活动等。其中会议活动资金五分之四来源于跨国公司（含金融机构），资金里三分之一强是国际投行注入的。<br />
 <br />
在广告发布方面，通过对其2008年第5、8、9期的样本分析，跨国公司在其刊登的广告页数分别为：35、40、45页，占广告总量的90%以上。<br />
 <br />
那些被“连锁”了的媒体<br />
 <br />
今年5月6日，《环球企业家》推出谢国忠的《救市是赌徒，熊市或持续18个月！》这次，谢国忠的文章没有在《财经》发表。<br />
 <br />
但仔细思考不难发现，《环球企业家》推出的两位总编中，总编辑李勇和胡舒立在《工人日报》有同事之宜，1998年《财经》创办不久，就投入胡主编的麾下，此后辗转高升，直至《环球企业家》总编。另一个执行主编叫杨福，2002年8月---2002年12月在《财经》任高级记者。<br />
当胡舒立在救市上推出“三不主义”引起举国震怒的时候，深谙 张弛知道的《财经》暂敛锋芒，而《环球企业家》此刻就有义不容辞的责任了！<br />
 <br />
我们要问，全国五花八门的媒体里，还有多少个“李勇”、“杨福”在辛勤工作呢？<br />
 <br />
5月6日，谢国忠反对“救市”的《救市是赌徒，熊市或持续18个月！》发表后，当日新浪网以《泡沫悖论》为题，将其挂上该网站财经频道首页，差不多同样时间，搜狐以同题目将该文在首页看出……该日，中国的专业财经网站如金融界、和讯、东方财富网无不以各样的标题，隆重刊出这篇文章。<br />
 <br />
同日，人民网、新华网在财经频道刊登《t望》杂志刊登的中国现代国际关系研究院经济安全研究中心主任江涌先生《境外利益集团在华游说活跃如何制约成关键》的文章却鲜有人问津，新浪不登，搜狐不理，各大专业财经网站更是视而不见，这篇思考总结几年来关于中国寂静问题的重要文章，几乎没有得到任何反响。<br />
 <br />
摩根的“经济学家”谢国忠难道比中国现代国际关系研究远经济安全研究中心主任江涌有更强的话语权吗？中国作家协会的《环球企业家》难道比新华社的《t望》更重要吗？<br />
 <br />
单凭这一个例子还不能说中国的互联网已经谢国忠话了，胡舒立化了、美国金融资本家化了，但是，不能否认，我们已经将新浪、网易、搜狐、等中国的有线门户全部卖给了美国人了，我们将空中网、中华网、华友世纪等中国的无线门户全部卖给美国人了，甚至我们将楼宇之间的广告传媒――分众传媒也卖给了美国了……<br />
 <br />
根据CNNIC的报告，到今天为止，中国的网民大概是2.4亿个，超过美国的2.1亿个。同样是CNNIC的第21期报告，到2007年，大约有73%的网民通过互联网阅读新闻。2.4亿网民，有1.75亿是通过互联网阅读新闻，可以认为，中国最有知识、最有财富、对社会最有影响力的社会中坚力量都在通过互联网了解着这个社会的信息。<br />
 <br />
然而中国人打开电脑，信息是美国人传送的，打开手机，信息是美国人传递的，站到电梯前，广告也是美国人传递的……这个重要的舆论阵地，我们也交到了美国人手中。<br />
 <br />
当互联网只剩下谢国忠、许小年、胡舒立的声音的时候，中国的百姓还有信心支撑中国的资本市场吗？<br />
 <br />
这些商业化网站，遵守中国的法律、政策。看起来规规矩矩，看起来和主旋律一致合拍，但是你无法让他刊登《专家：境外利益集团在华游说活跃如何制约成关键》这样的文章，你无法让他将夏斌做得比谢国忠重要。<br />
 <br />
随风潜入夜，润物细无声，就在着一点点的偏颇之间，他们就将天然的“信息不对称、信息不完全”，进一步人为地造成“完全信息部队称”。<br />
 <br />
到21世纪，国际、国内两个环境发生了巨大变化，新闻宣传也应随之相应变化。胡锦涛总书记今年6月份视察人民日报是，对新时期新形势下的新闻宣传工作做出了重要指示，他指出：“做好新闻宣传工作，关键在班子，在队伍、在人才。要大力加强新闻轩窗战线领导班子建设，把思想政治坚定、组织能力突出、熟悉新闻宣传工作、富有改革创新精神的优秀干部选拔到领导岗位上来，确保新闻你轩窗工作的领导权牢牢掌握在忠于马克思主义、忠于党、忠于人民的人手里，”<br />
 <br />
 <br />
责编：刘瑞</div>

							
							
							
							
