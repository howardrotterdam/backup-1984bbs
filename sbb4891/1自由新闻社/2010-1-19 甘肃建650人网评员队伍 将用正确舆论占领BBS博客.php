<br />
浙江自由作家温克坚说，中国一些地方公开雇用五毛党冒充民意欺上瞒下，是误导决策者的一种愚蠢作法。<br />
<br />
他说，「我想象不出来哪一个政府会需要通过制造这种扭曲民意来掩耳盗铃的作法。这简直是愚不可及。」</div>

							
							
							
							
