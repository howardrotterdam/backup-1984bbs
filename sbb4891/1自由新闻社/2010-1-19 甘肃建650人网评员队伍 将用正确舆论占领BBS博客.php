<br />
　　86个县区的新闻网站年内开通<br />
<br />
　　励小捷明确了2010年全省宣传思想文化工作的具体举措：积极回应人们普遍关心的就业就学、楼市股市、卫生医疗等热点，主动设置议题，引导群众正确看待热点，理性合法表达诉求；全省86个县区的新闻网站今年年内有效开通；做好2010“感动甘肃十大陇人骄子”评选活动，组织开展“争做道德实践先锋”主题活动和全省第二届道德模范评选表彰活动；对我省重大历史和现实题材进行梳理，面向社会征集戏剧重大题材创作项目；争取黄河剧院、金城第一戏楼建设早日完成；谋划好新的公共文化建设项目，对“社区书屋”和“社区和学校电影放映工程”等建设项目抓紧论证，争取早日列入“十二五”规划的盘子；推进党委、人大、政协、法检两院对外重要信息发布工作，协调重点企事业单位开展资助新闻发布，加强选题策划和议题设置，选择公众关注的热点问题，及时邀请新闻发言人“自主发布”。<br />
