<br />
　　【规划2010】<br />
<br />
　　全省逐步建立650人网评队伍<br />
<br />
　　今年我省要利用互联网强大的新闻聚合、在线互动等功能，加快网监、网评队伍建设，形成以50名网络评论“高手”为核心层、100名网络评论“好手”为紧密层、500名网络评论“写手”为外围层的网评队伍体系。<br />
<br />
