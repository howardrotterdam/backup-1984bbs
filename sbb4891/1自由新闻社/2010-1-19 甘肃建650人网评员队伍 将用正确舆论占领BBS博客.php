<br />
【中央社u台北25日电】 2010.01.25 04:42 pm <br />
<br />
中国甘肃省今年将聘用650位网络评论员，目的是「正确引导社会舆论」，大陆网友将这些受雇于官方、为官方帮腔的网评员称为「五毛党」。消息传出后，引起大陆舆论一片哗然。<br />
<br />
中共甘肃省委常委、宣传部长励小捷日前在宣传思想工作会议上表示，甘肃今年要利用网络强大的新闻聚合、在线互动等功能，加快网络监管、网评队伍建设，形成以50名网络评论高手为核心层、100名网络评论好手为紧密层、500名网络评论写手为外围层的网评队伍。<br />
<br />
励小捷说，网评队伍要「及时引导舆论，引导群众正确看待热点。」<br />
<br />
网评员通常扮成普通网民，主要任务是登入网站、BBS、部落格，了解网上信息，并针对热门问题及时发文，引导社会舆论<br />
<br />
据称，网评员每发出一篇文章就能领取人民币5毛钱的酬劳，因此被称为「五毛党」。<br />
<br />
甘肃省公布组建「五毛党」的消息后，网络上广泛转载，众多网友对此议论纷纷。<br />
<br />
大陆畅销小说作家韩寒在部落格中写道，「网络评论员也叫五毛党，乃是地下党。你把成立一支地下党的消息以及名单直接公布了，乃是大忌。」<br />
<br />
网友胡超在北京新浪网发文说，甘肃的做法不仅将五毛党合法化、等级化，从字里行间流露出来的意思是，「五毛党具有提拔为公务员的可能。」<br />
<br />
北京著名艺术家、网络评论人士艾未未对美国之音表示，五毛党现象在中国很普遍，各地都有。他说，「这些人基本上是属于出卖灵魂的人，他们为了获得一些小恩小惠，就开始做一些令人不齿的事情。」<br />
<br />
