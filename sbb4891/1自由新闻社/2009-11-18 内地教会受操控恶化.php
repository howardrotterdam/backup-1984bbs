陈日君：内地教会受操控恶化<br />
<br />
【本报讯】美国总统奥巴马在上海演说时提到的普世价值、宗教自由，在今日的中国仍然遥不可及。教廷国务卿贝尔托内上周向全中国神职人员发表公开信，重申梵蒂冈理解内地神父「复杂而艰难的境况」，提醒内地神父非孤军作战；天主教香港教区荣休主教陈日君枢机昨日发表文章，指教宗两年前向内地教会发公开信后，内地教会受操控的情况越来越严重。<br />
<br />
贝尔托内上任后首次向中国教会发表公开信，多番嘉许内地教会，「教会不会忘记在艰难岁月里，一些主教和司铎为教会作出了见证，甚至为教会和基督献上生命。」他表明教廷明白国内神父的处境，提醒他们：「你们不要认为你们是在孤军奋战。」<br />
<br />
不能再屈服在爱国会下<br />
<br />
香港教区荣休主教陈日君昨日则在网志发表文章，指近年内地教会的情况不但没有好转，反而更恶化，爱国会千方百计、威胁利诱，把教会奴化；他呼吁教会团结一致，拆除墙壁，「那道墙就是国内的天主教爱国会。」<br />
<br />
陈日君解释， 07年教宗本笃十六世给国内天主教会写信，本是个突破性的机遇，可惜两年后的今天，「他的信并未发挥它本该发挥的作用，教会的情况这两年不见好转。不正常的状况没有改善，更恶化了。」<br />
<br />
他解释，地上教会 07年 7月庆祝爱国会成立 50周年， 08年 12月又庆祝了首次非法祝圣主教 50周年，令人难以接受，他强调妥协不能无底线，「妥协不能无限期，是时候大家按教宗的信走出一步。」他要求主教神父不要再支持独立自办教会和自选自圣主教」，更不能再继续屈服在爱国会之下。</div>

							
							
							
							
