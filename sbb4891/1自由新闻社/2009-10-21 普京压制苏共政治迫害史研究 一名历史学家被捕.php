													<h2>普京压制苏共政治迫害史研究 一名历史学家被捕</h2>
						
						
																											<div id="postmessage_293910" class="t_msgfont">一名研究斯大林时期被俘德国人命运的历史学家近日被捕。此举表明俄官方刻意压制政治迫害的证据。<br />
Luke Harding<br />
guardian.co.uk&nbsp; &nbsp;2009年10月16日 <br />
<br />
seadral @ yeeyan 翻译&nbsp; &nbsp; 原文链接 <br />
<a href="http://www.guardian.co.uk/world/2009/oct/15/russia-gulag-historian-arrested" target="_blank">http://www.guardian.co.uk/world/ ... -historian-arrested</a><br />
<br />
最近，俄罗斯政府大肆压制对于斯大林时期的史学研究，一名调查二战中被苏联关押的德国人命运的历史学家被捕。<br />
<br />
这名历史学家名叫米克海尔・苏普伦（Mikhail Suprun），上月被俄罗斯安全部门官员羁押。他的公寓遭到查抄，所有的档案文件都被卷走。现在，他因侵犯隐私的罪名遭到起诉，如果判决成立，将面临四年的牢狱生涯。<br />
<br />
苏普伦研究的对象是那些被送往古拉格集中营的德国人。他是阿尔汉格尔斯克滨海大学（Pomorskiy university）的一名历史学教授，他的研究包括了那些战时被红军俘虏的德国犯人以及一些讲俄语的德国族裔，他们当中许多人来自俄罗斯南部，被斯大林放逐。两批人最终都被关押在阿尔汉格尔斯克的集中营里。<br />
<br />
“我计划写两本书。可能还要两三年时间才能完成，”苏普伦今天向《卫报》如是说。这位历史学家将其被捕一事形容为“十分荒唐”，他同时表示，他与地方官员签下协议，不过多谈论这个案子。<br />
<br />
然而，这次逮捕却在德国、特别是一些重要的历史学家当中引起了愤慨。俄罗斯政府正试图为斯大林翻案，并压制独立的史学研究――苏联时期的政治镇压以及古拉格集中营体制的受害者，现在成了人们禁忌的话题。<br />
<br />
历史学家、作家奥兰多・菲格斯（Orlando Figes）表示，苏普伦被捕这样的事情是前所未有的，它是“普京统治下压制历史研究和言论自由的行动”。菲格斯是伦敦大学伯克贝克学院的历史学教授，他补充道：“如果这意味着这个政权意图打击收集斯大林恐怖时期个人数据的行为，那么［这件事］也许相当有警示意义。”<br />
<br />
苏普伦的研究项目是同德国红十字会合作完成的。对于那些被作为战犯送往苏俄的德国人，该组织仍在试图探寻他们的命运――他们当中很多人一去不复返。德国红十字会今天表示，苏普伦被捕一事给研究项目造成了一些障碍。此外，德国右翼议员埃里卡・斯泰因贝克（Erica Steinbach）表示，此事“完全不可接受”。<br />
<br />
俄罗斯的联邦安全局情报部门同时逮捕了一名警官，他曾将当地内政部门的档案交给苏普伦。其中包括1945到1946年间40000名古拉格集中营的德国受害者名单，他们当时被驱赶到阿尔汉格尔斯克地区。在档案中，他们被送到哪间集中营，他们最后是否存活下来等情况，都有详细记述。这位名叫科尔・亚历山大・杜达耶夫（Col Alexander Dudaryev）的警官，被控犯有渎职罪。<br />
<br />
“我们看到了，操纵历史之行为死灰复燃，”“阿尔汉格尔斯克人权运动”的劳夫・加比杜林（Rauf Babidullin）如是说。“斯大林的镇压之惨烈，大多数俄罗斯人一无所知。当权者都是克格勃的人。他们可不想让人们知道克格勃的前辈们干的好事，更不想让人知道它们程度有多深。”<br />
<br />
问起阿尔汉格尔斯克冰天雪地中古拉格集中营的生活，他望着白海，说道：“糟透了。冬日无比漫长，营房里的条件差到了极点。幸存下来的德国战俘砍伐木头。在苏联的其他地方，他们修建房子，莫斯科国立大学就是这样建起来的。”<br />
<br />
阿尔汉格尔斯克的集中营中也有波兰人、希腊人、鞑靼人和其他一些非俄罗斯的民族。战事后期，有1100万德国人被关押在同盟国战俘集中营里，有100多万人死去。苏联战犯情况更差，570万被德国关押的战犯中，有330万死去――超过了一半。<br />
<br />
二战仍是俄罗斯和苏联解体后其邻国之间矛盾的主要根源。俄罗斯总统梅德韦杰夫反复谴责乌克兰、波兰和其他东欧国家，称它们歪曲历史，以达到政治目的。五月份时甚至成立了一个新的国家委员会，专门防止所谓的“歪曲历史的行为”。<br />
<br />
今年夏天，由巴尔干半岛国家领导的欧洲议会安全与合作组织通过了一项动议，将纳粹和斯大林主义画上等号，这让俄罗斯政府出离愤怒。同时，俄政府也为斯大林与希特勒签订瓜分东欧的莫洛托夫-里宾特洛普协定提出辩解，将它称为一次“策略性协定”，与其他西方国家纵容纳粹的行为并无二致。<br />
<br />
莫斯科“亚洲人权观察”组织的领导者埃里森・吉尔（Allison Gill）表示，苏普伦事件“让人非常烦恼”。“这代表了一种企图，对于俄罗斯是怎样的一个国家，今天又意味着什么，一些人希望只有一种看法存在。它否认了俄罗斯历史的复杂性和一个事实，即每一个人的经历都大不相同。我一点也不喜欢这样。”他说道。<br />
<br />
苏普伦的研究同过去二十年俄罗斯人权组织“纪念碑”进行的其他项目有些相似。“纪念碑”收集了数万名古拉格集中营受害者的姓名，在公开的“纪念册” 上披露他们的详细情况。去年警方搜查了“纪念碑”在圣彼得堡的办公地点，收缴了菲格斯在关于斯大林俄国的书《告密者》中使用过的一些材料，但后来又将其归还。</div>

							
							
							
							
