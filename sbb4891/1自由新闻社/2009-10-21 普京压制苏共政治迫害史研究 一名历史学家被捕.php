																											<div id="postmessage_294345" class="t_msgfont">俄罗斯能正视斯大林时代<br />
《卫报》报道了一名俄罗斯历史学家被逮捕的事件，这是一位俄罗斯读者对此报道的回应，他认为：俄罗斯的媒体、文化以及教育界已能正视斯大林时代的暴行了。<br />
<br />
<br />
<br />
Victoria Khromova<br />
guardian.co.uk&nbsp; &nbsp;2009年10月21日<br />
<br />
summerain @ yeeyan 翻译&nbsp; &nbsp; 原文链接<br />
<br />
在你们关于一名俄罗斯历史研究人员被拘捕的报道中，有奥兰多・菲吉斯（Orlando Figes）的评论，他把这次逮捕说成是“对自由的历史研究和表达的普京式压制运动。” （见10月16日的报道《俄历史学家因研究斯大林时代遭逮捕》）。它使我重新读了一遍菲吉斯此前的一篇文章，那篇文章说，他相信克里姆林宫想为斯大林恢复名誉，因此向其出版商施压使对方决定让他的图书下架。（详见3月4日的文章《是克里姆林宫让我的书下架了吗？》）<br />
<br />
我不知道克里姆林宫对于历史的非官方态度是什么。但是，我作为俄罗斯人，能够毫无阻碍地接触到俄罗斯的流行文化，千千万万的普通民众同样如此。我亲身经历过苏维埃时代的新闻和宣传，在我看来，显而易见的是，俄罗斯媒体在正视斯大林暴行方面已经有了长足进步。<br />
<br />
2004年普京任总统期间，有两部以斯大林统治时期为背景的小说被改编成电视剧并在黄金时段播出。其中一部是根据瓦西里・阿克肖诺夫（Vasily Aksyonov）1992 年的小说《莫斯科传奇》（The Moscow Saga）改编的，讲述的是一个家族的悲剧，这个家族与斯大林亲近，但被斯大林的恐怖和集中营迫害得家破人亡。它生动描述了人们――特别是秘密警察和党内的那些人――在妄想狂式环境中的道德毁灭，同时写到了斯大林崛起的过程，用西方人并不陌生的角度描述了这位独裁者。<br />
<br />
阿纳托利・雷巴科夫（Anatoli Rybakov）的作品 《阿尔巴特街的儿女们》（Children of the Arbat）一直受到压制，直到1987才得以发表。这部作品甚至描写了社会主义信仰者的失望、日益强烈歇斯底里情绪以及政治迫害氛围。<br />
<br />
这两部作品随处都可以得到，今年夏天我就在莫斯科买到了它们的DVD。而且这并不是个别例证。就连描写1950年代莫斯科摇滚青年亚文化的轻松电影作品《叛客青年》（Stilyagi）（2008）这样的作品也涉及到共产党政策对于包括从服装到音乐等一切方面进行的严厉限制所造成的影响。<br />
<br />
菲吉斯写道，他用来写书的材料被没收，这是俄罗斯对“历史题材的出版和教育进行更广泛意识形态控制的一部分。”他提到了当局“自己的教科书”：《俄罗斯当代史：1945――2006（教师手册）》。但俄罗斯教育部负责国家政治教育事务的副局长在上周的新闻发布会上说，没有只能使用单一教科书的计划，目前已经获得批准的教师用历史教科书有108种。她还说，教师有权采用自己的教学方法和教学视角，这是写入法律的。同时在2009年9月，《古拉格群岛》（The Gulag Archipelago）成为了高中生的必读书。<br />
<br />
我想，事实是否是，西方人发现自己很难容忍苏联的这段历史，很难接受俄罗斯人那种不道歉的方式和容忍态度。不可否认的是，共产党时代那些不愉快的真相已经被改革后时代的人们所吸收和接受了。这不会阻止他们向前走，而是让他们知道他们从哪儿来、他们的先辈们曾经忍受过什么。<br />
