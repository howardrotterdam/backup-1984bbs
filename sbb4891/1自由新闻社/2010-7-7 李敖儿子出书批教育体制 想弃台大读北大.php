																											<div id="postmessage_725408" class="t_msgfont">　　中新网7月7日电 据台湾《联合报》报道，大作家李敖后继有人，年仅17岁的儿子李戡今年同时录取台大与北大，而且一身反骨，出版的第一本书“李戡戡乱记”就是批判目前的教育体制。<br />
<br />
　　李敖一生风流，最后情定小他30岁的妻子王小屯，婚后生下一儿一女，取名为李戡和李谌。一直对家人极度保护的他，甚少让妻儿曝光。<br />
<br />
　　李敖昨天难得与儿子李戡一起上陈文茜的“文茜的，与我们的人生故事”节目，向来言辞犀利的李敖，也因宝贝儿子在旁，脸部线条柔和许多，陈文茜笑道：“李敖和儿子一起，变成侠骨柔情的爸爸。”<br />
<br />
　　李戡今年从师大附中毕业，当年以一分之差没考上建中，但在附中的自由校风中，于玩乐和学业中顺利取得平衡，保持优异成绩，但却对教育现况强烈不满，还说：“自己做了很多烂题目、看了很多烂教科书。”<br />
<br />
　　李敖曾表示，将儿子取名为李戡，“戡”字在古代有平定、消灭叛乱之意。自认受台湾教育荼毒的李戡，便在新书“李戡戡乱记”中，展现企图平定目前教育乱象的决心，他为推翻教科书中的内容，多次进出编译馆希望能找出理想的诠释，例如“二二八事件”在他的笔下，即衍生出有别现有史家的观点。<br />
<br />
　　对于儿子在文坛初试啼声，李敖预言：“儿子比我可怕，我只会越来越老，他会超越老爸，指日可待。”<br />
<br />
　　李敖年轻时最爱奇装异服，在台大校园里常身着一身长袍，陈文茜戏称他为“Lady Gaga”。但李戡穿着朴素，丝毫没有遗传到父亲的搞怪基因。李敖说：“我是反叛大王，儿子虽然正值青春期，不仅没有反叛我，反而对我多了点尊敬。”两人目前还连手在“李敖李戡，双双落网”部落格(博客)中发表文章。<br />
<br />
　　今年李戡考上台湾大学地质科学系，由于大陆新政策，台湾高中毕业生学测成绩达“顶标级”，即可免试申请，李戡因此也申请了北京大学，日前已通过口试。北大得知李戡背景后，强烈希望李戡能去北大读书。由于北大的大一新生不分系，这让文理都不错的李戡，还有时间考虑是否遵循爸爸的脚步，在文史的领域钻研。<br />
<br />
　　获悉同父异母弟弟李戡可能到北大念书，李敖旅居北京的大女儿李文昨天表示，她很欢迎弟弟去，会尽到姐姐责任，带弟弟融入这个国际大都市。</div>

							
							
							
							
