													<h2>电影著作协会打击网络盗版 向VeryCD提起诉讼</h2>
						
						
																											<div id="postmessage_767723" class="t_msgfont"><a href="http://www.mtime.com/news/2010/07/30/1437652.html" target="_blank">http://www.mtime.com/news/2010/07/30/1437652.html</a><br />
 <br />
中国电影著作权协会（影著协）及其有关会员单位日前在京召开新闻发布会，决定就上海宽娱数码科技有限公司（英雄宽频）、杭州世纪联线网络技术有限公司（世纪联线）、上海隐志网络科技有限公司（VeryCD）以及其他侵权网站的侵权盗版行为向人民法院提起民事诉讼，以切实保护自己的合法利益。<br />
<br />
　　据悉，7月21日国家版权局等三部正式启动“剑网行动”，目标是进行网络侵权盗版专项治理。但此后，上海“英雄宽频”和杭州“世纪联线”仍然继续提供《枪王之王》、《天下第二》等众多影片给北京的一些网吧使用，而这些影片的版权属于中国电影著作权协会的会员单位，并已授权给影著协实行集体管理。协会在取得大量确凿证据后，决定分批向管辖法院提起民事诉讼，要求立即停止侵权，并赔偿经济损失。<br />
<br />
　　众多影视公司代表应邀出席此次发布会。《叶问2》的制片人安晓芬就称，今年5月4日，电影《叶问2》刚上映不久，就发现上海隐志网络公司经营的“电驴”网站首页推荐侵权的清晰版《叶问2》。然后，盗版“种子”广为扩散，出现了网上疯狂盗播《叶问2》的局面。根据“电驴”的点击率、影院电子售票系统数字平台显示的票房流失量、中国发行放映协会出具的影院平均票价等数据，比较精确地计算出经济损失为人民币1185.08万元。安晓芬还说，“今年上半年有110部华语片在国内上映，但赚钱的只有13部，也就是说90%的片子都是赔钱。在这样的情况下，打击盗版就变得更为重要。”<br />
<br />
华谊兄弟代表<br />
<br />
　　华谊兄弟的代表孙建红则介绍，“就在《唐山大地震》上映前一天，就发现某网站已经出现该片的枪版视频；而在《谍海风云》上映三天后，影片也出现了盗版。目前网络盗版十分猖獗，已出现几乎无所不包的形势。”他还称，曾通过一项对网民的调查显示，如果没有网络盗版视频的出现，有大概42%的观众会选择去影院观看电影，而网络视频在几天之内就能达到上千万的点击量，给票房带来的损失可见一斑。因此他呼吁，所有影视公司都应该奋起维权，打击网络盗版。<br />
<br />
　　据影著协的调查，在18岁至35岁的年轻人当中，通过互联网观看电影的比例占其观看电影总数的50.5％；平均每人每年31.1部次；最高的达到每人每年500多部。可见，互联网已经成为青年人观看电影最主要的方式。影著协领导在会上表示，希望广大电影权利人、使用者和社会公众行动起来，把“剑网行动”贯彻到底，共同做好反击盗版的工作。</div>

							
							
							
							
