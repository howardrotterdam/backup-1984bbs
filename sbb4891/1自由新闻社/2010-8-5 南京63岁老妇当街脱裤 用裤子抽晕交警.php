63岁老妇用裤子抽晕交警 裤子一抽井井当即昏倒<br />
<br />
<br />
<br />
　　昨天下午1点左右，在南京市中央门立交桥下，一名骑电动三轮车的女车主张某，因骑反道违章带客撞到一骑自行车行人。交警六大队的一名交警赶来处理事故要求暂扣车时，该女车主突然脱掉裤子，用裤子抽打交警，直至将交警打昏在地。 <br />
<br />
　　事发现场位于南京中央门立交桥下方东南角。事发时，这名电动三轮车女车主，上身穿着浅蓝色短衬衫，下穿黑色长裤，骑着电动三轮车反道违章带客，一下子撞到途经此地的一骑自行车行人，随后双方发生纠纷引起交通不畅。正在附近巡逻的行人六大队交警谢斌和两名交通协管员赶到现场，谢斌发现女子张某所骑这辆电动三轮车无牌无证，属于非法营运，加上反道违章等，于是按规定要暂扣张某的电动三轮车进行调查。 <br />
<br />
　　接下来的一幕令人咋舌和吃惊。在众目睽睽之下，张某脱掉裤子冲向交警，抡起手里的裤子抽打谢斌。现场的交警谢斌、两名交通协管员顾及对方是女同志，又没穿裤子，并没有强行制止她的行为，而是不停地劝阻对方停手。张某看谢斌不还手，竟又转头将气撒在了停在一旁的警车上，抡起裤子打起了警车。这时，谢斌站在张某的电动三轮车前，用手抓住三轮车的车把，张某以为谢斌要带走她的三轮车，竟再次转头，拿着裤子又对着谢斌抽打起来。 <br />
<br />
　　见此情况，谢斌侧着身体任凭张某抽打。此时张某想推着车子离开现场，不住地推搡站在车旁的谢斌，见推不动，她再次扬起手中的裤子对着谢斌抽打。“啪”的一声，张某抡起的裤子抽在了谢斌的后脑勺。谢斌摸了一下后脑勺后，突然倒地。 <br />
<br />
　　随后，中央门立交桥派出所民警赶到现场，迅速将张某控制住。而受伤倒地的谢斌也被120救护车送往中大医院救治。当围观市民劝说张某，让她先把裤子穿上时，她坚决不穿，对于自己脱下裤子并抽打交警的行为，张某觉得自己很委屈，“我不穿，我怕哪个？我63岁的老太婆怕哪个，我大儿子要靠我老太婆养，我怕哪个？” <br />
<br />
　　由于在现场的都是男警官，不方便处理，派出所又找来一名女民警和一位女保安，她们到了之后，又费了一番周折，终于给张某穿好裤子。带到中央门立交桥派出所处理。据在现场处理此事的派出所保安介绍，这已不是张某第一次脱裤子，以前也曾发生过这样的事情。 <br />
<br />
　　中央门立交桥派出所有关人员表示，因张某涉嫌妨害公务，故对其作出行政拘留15天的决定，至于是否属于袭警要看交警谢斌的伤情而定。 (来源：新华报业网) <br />
