																											<div id="postmessage_772353" class="t_msgfont">脱裤子的老人&nbsp;&nbsp;你是谁的母亲？<br />
菁菁远山<br />
<br />
 <br />
&nbsp; &nbsp; 有一种驿动，它是良心发现后的震颤。当我的心灵在铁色的法律一隅逼仄地站立，情感的潮水便奔涌漫漶。当一切了无声息，那又是一种怎样的凄楚和莫名。做为一个对法律并不生疏的人来说，内心的矛盾纠结才是痛苦的根基，也许没有这种经历的人，没有对民生艰难的深刻体察的人是不会了解我的心境的。当然，交警和城管是这个社会不可或缺的，因为我们的社会需要秩序，但我的秉性和特质却告诉我，我并不适合做这个工作。这是我看到新浪网的一段视频时想到的。<br />
<br />
&nbsp; &nbsp; 就在昨天，我在网上看到了这样一段视频，而这一情景就发生在我们的曾经的首善之区――南京。8月3日下午1点多钟，在中央门立交桥下，一位63岁的母亲骑着马自达三轮车，因反道违章带客撞到一辆自行车。交警六大队的一名交警赶来处理事故要扣车时，她突然脱掉裤子抽打交警，但这样一个并不激烈的抽打却让交警昏倒在地。但让我无语的是，她在“行凶”的时候，总是不时地护住她赖以生存，比她性命和尊严更为宝贵的三轮车，因为那是她的饭碗，甚至金贵的无法比拟，在这个冷漠的社会，这就是她活着的资本。随后，她被带到中央门立交桥派出所做进一步的处理。她的脸庞黝黑而沧桑，她的背影孤立而支离，由此，我也看到了当一个人的重要财产失去后，那是一种怎样的懊悔，无奈，恐惧与悲凉。我的内心不免有难以名状的凄苦，这个以三轮车为营生的母亲，在与她的儿子年龄相仿的执法者面前，为什么没有了羞耻感，对他们如此“逞威”？你是谁的母亲？<br />
<br />
&nbsp; &nbsp; 有人说：她只是想凭自己仅有的能力争一口饭吃，何罪有之有？！这种说法自然不对。一个社会需要秩序。对个体而言，遵守规则是每个公民的责任。但是，违反规则的行为在有意和无意间还是会发生的，因此，维护秩序就成了政府和执法部门的天然职责，也是形成健康社会的必然要求。但是，只有健康社会才能打造健康的公民，在一个公平缺失，民生凋敝的社会，执法的成本是惊人的。老人的举动是不对的，民警们的执法是文明的，但这位母亲背后折射出的民生则颇让人玩味。思忖多时，我想起了这样一个在中国流行了数千年的一个词：人穷志短。<br />
<br />
&nbsp; &nbsp; 俯瞰大地，哀民生之多艰；仰望星空，叹公平正义之不逮。孙公的“三民主义”在中国已经唱响100多年，如果能跨跃时空，让他在穷其毕生试图改变的土地上驻足，留给他的将会是一种什么样的心境？是的，“黑砖窑”里的苦工，山木集团里的性奴，富士康工厂里的“机器人”，山东那些在酷暑中倒下的农民工……孙先生曾经为我们编织了一个美好的社会前景，但尽管付出了无量头颅和血流飘杵的代价，还是没有给人们带来胎动的喜悦。令人唏嘘的是，改革开放以来，中国的国力大增，就GDP而言，据说现在已经成了全球“老二”，今年的税收将达到8亿，但穷人群体却无限膨胀。经济学家们说，中国的中产阶级正在壮大，社会也将会形成“橄榄型”结构，但事与愿违，白领阶层却沦为蚁民，尊严扫地。我们的社会结构好比“亚铃”，一端是权贵，一端是贱民，两者几乎不流动。而在他们的中间，则是收缩成“一根棍”的所谓中产阶级，几近忽略不计，这是何其危险的一种结构？也许，正是这种结构，才造成了天价维稳的事实。<br />
<br />
&nbsp; &nbsp;中国目前究竟是一个什么社会，对这个问题的回答是见仁见智的，但权贵当道却是不争的事实。腐败的已经深入致到社会生活的各个方面，没有监督和排斥监督、权力干涉司法使公平正义不能彰显。要改变这种状况，解铃还需系铃人，就是用权力来规制权力，推进民主宪政进程，铲除腐败产生的根源，实现社会公平正义。而目前最需要的是，清除腐败，平息民怨。但让人失望的是，做为反腐中间环节的“官员财产向社会公开”成了久孕不产的“准死胎”，除了给人们带来“永远的希望”之外，似乎没有什么功用。<br />
<br />
&nbsp;&nbsp;在中国的新一轮严打中，“扫黄”成了重头戏，“小姐”一词又热络起来。众所周知，有钱人是没有人去当“小姐”的，他们在某种程度上是弱势一族，是生活所迫。但与腐败分子而言，“小姐”可能不比他们“更坏”，因为贪官们是强盗，同时也是打手，他们的不义之财是全体公民的，他们的权力也是属于人民的。同时，有层次的贪官是不膘的，他们可以用权力和金钱让中意的女人就范，对此，广西那个烟草系统的局长，在他的“局长日记”里就揭开了这个秘密。风声水起的扫黄大戏仍然在上演，但这个世界上只要有男人和女人，这种现象就会存在，为了生存，一些“小姐”也背井离乡、漂洋过海了，据马来西亚《光明日报》报道，东北区警方反风化、肃赌及私会党取缔组警员，于今年8月3日凌晨12时30分，突击检查浮罗池滑某KTV娱乐中心，当场扣查34名中国籍女子，警方怀疑，这些中国女子是在此中心当陪座女郎。当然，黄赌毒是社会丑恶现象，扫黄是社会共识，但如果是过去的那种“割韭菜”似的扫黄，是不能达到源头治理的目标。但从“小姐”的泛滥上看，在某种程度上也折射出了民生的困窘。<br />
<br />
&nbsp; &nbsp; 我的心里放不下那个老妇，圈子兜了这么半天，还得绕回来。从这个老以三轮车为谋生手段的妇人身上，我们可以看到这个老人辛酸的一面，但他并没有放弃对生活的希望，这是一种怎样的坚守，我们应该如何捍卫他的生存权，这是一个法律之外的话题，也是一个沉重的话题。当那些她“孙子辈”的富二代们正开着宝马奔驰在大路上肆意妄为时，你重新再打量一个这个应该是早已经到退休年龄的老人，如果只有“泼妇”的形象，那么你就像陈文茜眼中的韩寒一样，“说话像放屁一样轻松”。当然，老人是违反了交通规则，但她毕竟是女人、是母亲、是公民，是一个接近风烛残年却又不得不用三轮车来谋生的贫民，是一个对法律无知而用极端的方式捍卫自己谋生手段的老人。从这个意义上说，“法律是无情的”确有商榷之处。我想，只有让良心成为“自由裁量权”的守望者，法律才能从铁色中看到些许的人性，更何况，在我们这个道德沦陷良知缺失的社会，法律和执法者如果失去人性的温柔一面，那么社会就会变成一个充满戾气的残暴的社会。<br />
<br />
&nbsp; &nbsp; 诚然，法律是公平正义的最后一道防线。我真不知道老人对交警们是如何看待的，也许，在她的心目中，他唯一的资本将被没收是唯一的处置结果。也许，在她的心目中，只能不择手段捍卫自己的财产。从视频上看，警察们的执法行为是文明的，但究竟如何处置却是未知数。我看到一个网友对此事的评论：“明显一个耍赖的老妇。都像她这样倚老卖老目无法纪还有什么次序可言，不要说什么没有活路就可以非法营运。不怕死的贩毒去啊，看不枪毙你，老人就可以无法无天了么？换了美国警察早开枪击毙了！！！”由此，有关三轮车引发的事件重新展现在我的面前。几年以前，湖南衡阳的一个城管官员，因为在执法中以强硬的手段没收了一个残疾人士赖以为生的改装机动三轮车，结果导致了一场社会悲剧的发生。残疾人士无路可走，找到了执法官员，用汽油浇到自己和官员的身上，最后同归于尽.；而2006年，一个年仅37岁的城管队长李志强，在强行没收一个名字叫崔英杰的卖烤肠小贩赖以为生的三轮车时，被崔英杰手刃身亡。我们从这两起事件可以看出，这些生活在底层的穷人，当他们赖以生存的最后&nbsp; &nbsp;底线不复存在的时候，他的最后的一线希望破灭了，此时，他往往会做出极端的事情。 <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;当然，任何社会都有穷人，经济的发展和制度的完善可以最大限度的减少穷人。但我们这个社会却是例外，在这个生产了世界上大量廉价产品的国度，穷人阶层却有扩大的趋势。有时我们会问：是谁吃了我的奶酪？当然是富人，而权力则是最后的邦凶。中国糟糕的制度设计花拳绣腿式的民主使得公平正义荡然无存。而那些在不公平的环境中成就的成功人士，他们不仅不知恩图报，反而加速盘剥层层加码，造成了严重的贫富不均，使得中国的的基尼系数早已经超过了0.40的警戒线，达到了0.50左右。这也是极端事件频发的根本原因了。那么我们这个社会是如何对待穷人的呢？在官员的眼中，有一种“贱民思维”，他们往往把穷人们的合理诉求当做无理取闹，从而让这些人成了维稳对象疯狂打压，这种思维的结果就是把社会打造成了一个没有出气口的“高压锅”，随时都有爆炸的危险。近年来频发的群体事件，也为这一 观点提供了事实上的佐证。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;我们在看一看“万恶的美国”是如何对待穷人的。北京时间2010年1月20日，美国总统奥巴马一家在马丁&#8226;路德&#8226;金纪念日，为150多名无家可归者提供午餐。第一夫人米歇尔、米歇尔的母亲玛里安&#8226;罗宾逊、奥巴马的两个女儿萨莎和玛莉亚一起在华盛顿一个名叫“所有人都能吃上饭”的餐厅为大约150名客人提供了午餐。奥巴马与志愿者合影留念并与那些在中心吃饭的人们握手。奥巴马的家庭成员随后担任起了端盘子、倒咖啡的工作。如果说这这是政客作秀的话，下面的一例子对中国人来说简直是天方夜谭。微软创办人盖茨把自己580亿美元财产全数捐给名下慈善基金比尔及梅琳达盖茨基金会，一分一毫也不会留给自己子女。他说：“我和妻子希望以最能够产生正面影响的方法回馈社会。”而且，在美国，这样的富人是不可攸举的。同在一个地球上，中国的官员和富豪怎么就这么差劲呢？而那些既想当婊子又要立牌坊的“诈娟们”事件的主角，就更让人惊叹弗如了。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;那个老妇人的影子总是在我的脑际萦绕，我的眼睛不时的噙着泪水，这是有别于余大师化学成分的眼泪，绝不是煸情的眼泪。因为这个老妇人在我的眼中，是一位母亲、是一个自食其力是劳动者、是一个坚强的女人，当然，她违反了交通规则。<br />
<br />
&nbsp; &nbsp; 写到这里，我些人难免会问，你写了这么多，究竟想表达什么？当然，我一是在拷问我们社会发展的同时，为什么穷人群体没有减少。二是想让大家学会如何对待穷人。</div>

							
							
							
							
