																											<div id="postmessage_263993" class="t_msgfont">复旦大学调查显示9成大学生自认为较爱国<br />
2009-09-29 17:47:50　来源: 中国青年报(北京)<br />
核心提示：复旦大学日前发起一项“上海大学生的中华人民共和国印象与记忆”的问卷调查，调查显示，90％以上当代大学生认为自己较爱国，近三分之二的学生认为，比起上几代人，大学生群体并没有显示出爱国程度的降低。<br />
<br />
中国青年报9月29日报道 日前，复旦大学公布的一项调查显示，90％以上当代大学生认为自己较爱国，近三分之二的学生认为，比起上几代人，大学生群体并没有显示出爱国程度的降低；超过80％的受访者认为，在不同时代背景下，比起上几代人，自身在爱国情感的表达上或多或少存在差异，更趋向于理性表达。 <br />
<br />
据复旦大学新闻学院副教授廖圣清介绍，这项主题为“上海大学生的中华人民共和国印象与记忆”的问卷调查，由该院精确新闻报道课题组在上海随机抽取19所大学950名大学生完成，调查回收有效问卷906份。 <br />
<br />
调查结果显示，当代大学生对自身的爱国程度较为认可，表示自己“非常爱国”和“比较爱国”的分别占到了33.0％和57.9％，总计达到了90.9％。同样，每一个学生个体对整个大学生群体的爱国意识认可也很高，达到69.8％。 <br />
<br />
面对“当代大学生爱国程度比不上老一辈”的质疑，近三分之二（63.5％）的大学生表示了否定，他们认为以“80后、90后”为主体的当代大学生相比上几代并没有显示出爱国程度的降低。 <br />
<br />
尽管受访者认为自己爱国意识强烈，但84.2％的被调查学生也承认，与上几代人相比，自身在爱国情感的表达方式上或多或少的存在差异，有48.2％的大学生表示自己趋向于“理性表达爱国情怀”。 <br />
<br />
复旦大学新闻学院大四的张同学表示，“爱国不一定就要喊口号、参加‘革命’，比如支持我国文化产业也是爱国的方式。”她说，“我们这一代大学生童年时代看的全都是日本或美国的动画片，难免感到伤心。” <br />
<br />
此外，在国际交流愈发频繁的今天，大学生也更多通过“向国际友人加强国家形象宣传”（43.8％）来表现自己的爱国情怀。复旦大学微电子系本科三年级的杨同学说，在美国交流期间，他总是告诉班里的同学，中国已经发生了翻天覆地的变化，并且每每说出这些话的时候，心中就充满了自豪感。 <br />
<br />
大学生和上几代人之间爱国程度的差异，可能很难衡量，但可以肯定的是，出生在改革开放后的这代大学生正用崭新的方式表现他们对祖国的无限爱意。 <br />
<br />
调查数据显示，73.2％的大学生对共和国未来的发展持正面积极的态度。数据同时显示，具有越高爱国意识的大学生对国家的发展前景有更加积极的态度。正像复旦大学生命科学学院本科四年级的朱同学说，“我相信未来的共和国会更加注重自己的软实力，在文化与思想的输出方面有长足的发展，在世界上也会有更多来自中国的声音。” <br />
<br />
用上海大学应用化学系谢同学的话说，“共和国走过60个年头的确艰辛，但我相信他还会走过更多更灿烂的60年，还会创造更多地辉煌，而我们也是其中的缔造者。” (本文来源：中国青年报 )</div>

							
							
							
							
