													<h2>武汉农民自制土炮轰退百人强拆队</h2>
						
						
																											<div id="postmessage_658948" class="t_msgfont"><a href="http://news.163.com//10/0607/07/68ICNAF000014AEE.html" target="_blank">http://news.163.com//10/0607/07/68ICNAF000014AEE.html</a><br />
<br />
<img src="http://img1.cache.netease.com/cnews/2010/6/7/2010060708340465507.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
杨友德在哨楼上演示自制土炮<br />
<br />
湖南在线6月7日报道&nbsp;&nbsp;为了反对强制拆迁，东西湖一农民自学“阿凡达”，在自己承包的田地里搭了个“炮楼”，自制火炮两次打退拆迁队。昨日，农民杨友德告诉记者，自己并不是钉子户。<br />
<br />
8米高“炮楼”守卫25亩土地<br />
<br />
昨日下午，记者在东西湖金银湖汪湖嘴，见到了56岁的农民杨友德。<br />
<br />
杨友德在这里承包了25亩地，2029年到期。在这片田地里，杨友德开展多种经营――养鱼、养牛还种植棉花和瓜果。<br />
<br />
记者在他的带领下，穿过一道小门便进入了杨友德的“领地”。为了阻止拆迁队，杨友德在田地里的小路设置了路障。他说，遇到紧急时刻可以燃烧这些路障，减缓拆迁队进来的速度。<br />
<br />
田地里建有几间平房，是杨友德和老伴的住所。在平房旁边，一个用木头搭建的“哨岗”特别引人注目，“哨岗”高约8米，下面架空，上面是一个不到3平米的“阁楼”。杨友德说，这是他的“炮楼”，居高临下，可以将25亩地尽收眼底。<br />
<br />
礼炮做成“火箭弹”射程达百余米<br />
<br />
这座“炮楼”没有楼梯，杨友德搬来一个竹梯才能上去。<br />
<br />
记者在上面看到，“炮楼”上放着价值2000多元的礼炮，杨友德将它们拆成单个，一个筒子就是一枚“炮弹”。说着，杨友德拿出一个礼炮筒子，将它放在一个自制的铁筒内，随后他一手握铁筒下面绑定的木棍，另一手点燃礼炮引线。“轰”一声，“炮弹”飞出去，在百米外的空中爆炸，威力惊人。<br />
<br />
曾打退百人拆迁队<br />
<br />
据杨友德说，去年他听说自己的25亩地被征用了，但是由于补偿没有谈妥，他拒绝搬出。<br />
<br />
“后来拆迁方就多次放话出来，说要对我动手。”为了保证自身安全，今年年初，杨友德将一辆手推翻斗车的前部铁皮拆掉，在翻斗里面放置了一箱礼花弹，准备对抗拆迁队。<br />
<br />
今年2月6日，30多人的拆迁队伍来到地头准备强征。“我当时就点燃了礼花弹。但是他们都躲在铲车后面，等我的礼炮放完了，他们冲出来把我打了一顿。”当时对方虽然没有强行将杨的房屋推倒，但放话说下次会再来强拆。事发当天，杨在亲友的帮助下做了“炮楼”，改装了“武器”。<br />
<br />
据杨友德说，5月25日下午，又有一支一百多人的拆迁队，戴着钢盔拿着盾牌，在推土机和挖掘机的掩护下，再次来到他的地里。“我发现后立即爬上炮楼，朝他们放了几炮，他们就停住了。后来民警赶来把他们赶走了。”<br />
<br />
心声“我不是钉子户”<br />
<br />
在记者采访过程中，杨友德反复强调，自己并不是钉子户。“钉子户是那些要价过高的人，我的要价是按照政府政策来的。”<br />
<br />
杨友德说，他将继续采用这种手段和拆迁队对峙。为此他又“研制”新型的近距离攻击武器“汽油弹”，并在“炮楼”上放置了躺椅，准备24小时守卫自己的田地。<br />
