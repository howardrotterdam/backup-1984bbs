																											<div id="postmessage_146583" class="t_msgfont">中国共产党可以说是个有远见的政党。至少在上世纪八十年代，官版政治教材就把战国时期名家哲学的“白马非马”钦定为唯心主义诡辩，早早地从根本消除了对改革开放三十年，特别是后二十年有关“羊头狗肉”的争议。 <br />
<br />
但无论在建国后的历史上还是今天，名不副实都是中共一个有目共睹的特征。 <br />
<br />
中共1949年夺取政权之后，毛泽东实际上把中共变成了他个人专权的工具，不仅中国普通百姓的民权、民生没有了着落，就是党员、党的干部、甚至高级干部如刘少奇、邓小平者亦不免被批判、打倒甚至折磨致死的命运。 <br />
<br />
&nbsp;&nbsp;中共的核心是一个“利益集团”，而这个集团的利益和普通中国人、和普通党员、甚至和党的领导者的利益都很难说是一致的。 <br />
 <br />
改革开放后，在“猫论”的指导下，中共热情拥抱西方投资和市场经济，让中国人，包括很多为共产主义奋斗了终身的中共党员重新体验了150年前马克思笔下“浑身滴着血和肮脏的东西”的资本主义。 <br />
<br />
代表谁？ <br />
<br />
7月1日，中国共产党成立88周年之际，中共自己公布的最新党员人数是7593.1万人，但这个总数本身很难说明中共的力量所在。 <br />
<br />
我们从更详细的统计中看到，中共党员中真正的工农已经不到一半，而党政机关、企事业管理和其他专业人员，也就是中国常说的“干部”达到2300万。同时，统计显示，有358万党员“在非公有经济中工作”，为私人老板打工。 <br />
<br />
<br />
 <br />
很多普通党员其实也是中共政策的受害者。 <br />
<br />
这两个数字可以说是今天中共性质和所处经济政治环境的最佳写照。这两个数字也注定了中共目前所面临的来自左右两方面的压力：左派指着下岗工人和血汗工厂里的打工妹说，中共背叛了共产主义理想；右派则痛陈中共腐败专权，要求实行民主宪政。 <br />
<br />
但问题是，今天的中国共产党不仅在理论和实践上放弃了马克思主义的政治底线，而且在今天中国的政治制度下也很难说中共还是一个严格意义上的政党：因为它不需要面对选举的压力，不需要为选民负责。 <br />
<br />
也许更准确的说法应该是中共，至少中共的核心是一个“利益集团”，而这个集团的利益和普通中国人、和普通党员、甚至和党的领导者的利益都很难说是一致的。 <br />
<br />
我的顶子，你的江山 <br />
<br />
这方面我们也许可以从一些活生生的地方官员身上看得更清楚：逯军，河南郑州规划局副局长，质问记者“你是准备替党说话，还是准备替老百姓说话”；李俊虎，河南偃师国土局局长，网帖爆出其“雷人”放话：“人民网算什么东西，那是电子垃圾”，害得中共党报《人民日报》的“人民网”在7月1日的有关报道还在表示“将继续关注”。 <br />
<br />
<br />
逯军不过是说了句实话，而尽管那位李局长出面否认，但视党报为垃圾，视中南海政令如废纸却不能不说是今天中国地方党员干部的一个普遍思维。因为他们既不用向选民负责，也不需要向党的领袖效忠，他们只要打点好自己的顶头上司，剩下的就是如何捞取个人利益了。 <br />
<br />
&nbsp;&nbsp;<br />
如何看中共88年来的发展和变化&nbsp;&nbsp;<br />
<br />
宣统年，满大人们对自己顶子的眷恋让清末的立宪最终变成了闹剧，使革命成了必须，让大清丢了江山。 <br />
<br />
如今，中共如果任由自己身居官位的党员欺上瞒下、为非作歹，换来的恐怕不仅仅是民间“草泥马”的怨言，中共自己这匹“白马”也好“黑马”也好，都不无“卧槽”的可能。<br />
