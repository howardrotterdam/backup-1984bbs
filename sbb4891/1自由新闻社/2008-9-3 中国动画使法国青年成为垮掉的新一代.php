																											<div id="postmessage_573" class="t_msgfont">中国动画使法国青年成为垮掉的新一代<br />
<br />
26岁的皮埃尔・布朗热是一名住在蒙彼力埃市的普通青年，他从来没有过工作经历，住在父母家，靠父母的养老金和每月领到的生活费过活，在法国，他是被称为“中国动画宅”的人之一。<br />
<br />
皮埃尔・布朗热住在一间八平方米的小屋里，房间里除了一张床，就只有电脑和电视及中国生产的EVD机，墙上贴满中国动画的海报。每天早上7：30分，他都会准时起床，打开电视5台观看中国动画《蓝猫淘气3000问》。这部动画每天早上7：30都会在电视5台播放一集，至今已经播放了2230集，皮埃尔・布朗热从19岁到现在看了6年多，从来没有落过一集。为了看这部动画，他放弃了很多工作机会，因为那些工作都要求8点钟准时上班，这时间明显和《蓝猫淘气3000问》发生了冲突，为此他毫不犹豫的选择了在家看电视，而不是出去工作。这部来自中国的超长寿动画还要两年的时间才会播放结束，所以至少在28岁前，皮埃尔・布朗热绝对不会出去工作。<br />
<br />
8点钟，《蓝猫淘气3000问》播放结束，皮埃尔・布朗热的一天才算正式开始。<br />
<br />
在使用全套印有《大头儿子小头爸爸》（这是另一部曾经在法国很热的中国动画）图片的餐具吃完母亲为他准备的早餐，他再次缩回自己的屋里，直到中午之前，只有需要上厕所时才会偶然从房间里走出来。<br />
<br />
回到房间里，他会打开EVD机，放进一张《封神榜传奇》的动画光盘――他之所以会选择购买中国的EVD机而不是SONY的蓝光机，是因为几乎所有的中国动画光盘都是EVD格式，包括蓝光在内的任何媒体都不能兼容，所以EVD机几乎占据了整个法国市场，而蓝光机则是滞销的―不过，他并不是要开始看动画，他需要的只是电视屏幕上一直播放中国动画。<br />
<br />
下面，他打开电脑，点开MP3收藏夹，这里有将近3万首中文动画主题歌曲，有些是中国动画的歌曲，有些则是中国方面翻唱的外国动画歌曲。皮埃尔・布朗热最喜欢的是中国翻唱的日本动画《新世纪天鹰战士》的主题歌《勇敢的少年》，歌唱者是因这部动画而迅速走红世界的著名声优鞠萍，一般他会在整个上午反复播放这首歌达几个小时，今天也不例外。<br />
<br />
在“美丽的天使在远方召唤你，勇敢的少年啊，快去创造奇迹！”的歌声中，皮埃尔・布朗热开始拼接他的鬼子驻扎营地模型。这个模型是3D动画《帽儿山的鬼子兵》（又是中国动！）中日本鬼子的营地。这部三维动画中精致的画面至今仍然是世界各国电脑动画教学的范本，《怪物史莱克》的美国梦工厂主管卡赞伯格在看过这部动画后心灰意冷，立即解散手下的制作团队，自己也转行去做了清洁工，可见这部动画的3D水准之高至少超过了怪物史莱克》十年水平。<br />
<br />
为购买中国义乌生产的鬼子驻扎营地昂贵模型，皮埃尔・布朗热甚至逼迫父亲卖掉汽车，所以他对这个模型格外珍爱，发誓要在一个月内将它组装完毕。可是，此模型包括88个螺丝钉在内的零件多达204个，非常复杂，他已经组装了将近两个月，到现在还只组装了一半。在制作完成后，他会把它放在客厅的正中央。<br />
<br />
组装模型的工作持续到中午12点，皮埃尔・布朗热放下手的工作，开始看每天中午播放的中国动画《哪吒》（还是中国动画！），这时即使母亲来叫他出来吃午饭，他也不会出房间，所以母亲只好将食物给他端进屋子里。<br />
<br />
吃完午饭，皮埃尔・布朗热终于走出了房间，顶着7月的晒人的阳光来到大街上。他的目的地是三条街外的书店，今天是中国动画资讯杂志《新干线・重动画》的上市日期，他要赶在卖完前去购买。自从法国ZF放开中国动画刊物的法文版审批手续后，大量中国动画杂志在法国出版，其总体销量已经远远超过法国报业发行量的总和。《新干线・重动画》是一本完全介绍中国动画资讯的杂志，皮埃尔・布朗热买到这本杂志后，进入旁边一家中国人开设的中国动画主题茶馆“大脸猫茶餐厅”，一边享受着冷气，一边观看这期的杂志。本期杂志上最令他感兴趣的是电影动画《宝莲灯》即将推出第46部续篇，不过他现在比较担心这部年底播放的中国动画电影影票过分热销，即使通过网络预订也会难以买到。<br />
<br />
薄薄一本48页的杂志看完后，皮埃尔・布朗热开始和周围的朋友讨论最近的中国动画讯息，这里的人都是和他一样的“宅”，也都是他的朋友，每当《新干线・重动画》发售时，他们都会聚集在这里。有时他们会为了一些例如“《快乐星猫》（自然依旧是中国动画）里星猫12星座战士谁最可爱”之类的问题发生激烈争执，有时甚至会为站在不同动画人物立场而大打出手，之后老死不相往来，不过这并不能影响他们对中国动画的热忱。<br />
<br />
在“大脸猫茶餐厅”度过整整一个阳光灿烂的下午，当太阳开始下山时，皮埃尔・布朗热与他的伙伴们陆续结帐回家了。不管他们今天的讨论是否有最终结果（实际上他们的讨论永远象英国下议院的会议一样喋喋不休且没有结果），他们都必须要回家，晚间黄金动画档正在等着他们。<br />
<br />
晚间黄金动画档对他们这些宅来讲是比生命还重要的事，从18点到20点的整整两个小时的时间，完全被中国动画所充斥，他们必须在这两个小时里坐在电视屏幕前，哪怕一秒钟也不能缺席。为了照顾皮埃尔・布朗的这个习惯，父母将家里的吃饭时间延后到20点，只要皮埃尔・布朗不从房间里出来，他们就绝对不会开饭，这已经成了这个家庭的生活习惯。<br />
<br />
晚饭后的时间，皮埃尔・布朗又会钻回自己的房间，打开电视播放中国动画光盘，自己则开始晚间的工作――开始玩网络游戏。皮埃尔・布朗12岁从一款叫做《学习雷锋》的中国游戏认识了网络游戏，现在已经有14年的游戏龄――在中国网络游戏占领100%世界市场的今天，已经没有一个会上网的欧洲人没有在玩中国的网络游戏了――他之前玩过8年的《热血传奇》，由于每天苦练8小时以上也没能上50级而被他放弃；后来玩了3年的《绝对女神》，又由于游戏里代理公司每天卖特价包、高等级道具，最后代理公司卷款逃跑而终止。<br />
<br />
午夜12点钟，皮埃尔・布朗在看完两集联播的深夜档动画《蓝猫虹兔七侠传》和成人动画《我为歌狂》（这两部依旧都是中国动画）后才会去睡觉。之所以会有深夜动画档，是因为《蓝猫虹兔七侠传》和《我为歌狂》都属于成人动画，其中《蓝猫虹兔七侠传》更是有着血腥的流血场面，所以会被错开在儿童活跃时间之外的午夜播放。但是，这还不是皮埃尔・布朗一天的结束，他将闹钟定在了凌晨两点，到那时他还要按时起来观看《天眼神虎》，那是一部被定位为恐怖类的中国动画。<br />
<br />
我不知道现在的法国青少年都在想什么，他们一定已经忘记了拿破仑时代的历史，深深植入他们脑海的只有《隋唐英雄传》给他们灌输的中国情结，因为那时的中国人和我们法国人一样有着金色或者红色的头发。他们一定更有兴趣了解猪猪侠（中国动画主人公）的生平而不是戴高乐，虽然后者是我们的复国英雄，而前者只是一头猪。<br />
<br />
中国动画和网络游戏的大量涌入，正在毁掉整整一代法国人，他们迷恋于中国的动画明星，对外界一无所知且对社会一无贡献，对国家的命运更是漠不关心。如果有一天中国人进攻法国，只要他们戴上蓝猫或者虹兔的头套，相信我们的青年人怎么也不会朝他们心中的偶像开枪，那样法国也许会再次象1940年那样沦陷，并且比那时更糟。德国是不可能以他们的纳粹主义征服法国，但现在的法国青年却早已拥有了中国人的思维，这一切都是拜中国动画和网络游戏所赐。<br />
<br />
一些有识之士已经开始讨论法国青年的未来，并提出了限制中国动画进口和18点至22点禁播出中国动画的提案，也许这会是挽救下一代的唯一办法。</div>

							
							
							
							
