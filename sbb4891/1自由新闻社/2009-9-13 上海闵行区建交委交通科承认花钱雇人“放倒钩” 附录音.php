转载-当放“倒钩”成为打“黑车”部门合法手段<br />
<br />
<br />
诸位车友，下文中或者您能了解的任何渠道中，我们的闵行城市交通执法大队都从来都是不承认有“倒钩”的。&nbsp;&nbsp;可是我有建交委-你们上级主管部门承认给“协案人员”的录音啊，也有你们建交委交通科万科长说那不是“倒钩”是一群富有正义感的社会人士的录音啊，如假包换的如非他们本人所说我情愿付法律责任。我愿意想媒体公布，请专家鉴定是否是建交委领导所说。<br />
<br />
前几天《新闻晨报》刊登了一篇《免费搭载邻居被指是黑车营运》文章，说得是有位住在城市边远小区的面包车驾驶员从家里出来，在车站处开双跳灯，示意可以把同小区的邻居免费带到莘庄地铁站。停车后，上来六个人，车将到地铁站口时被城市交通执法队人员拦下。说是“黑车”，罚一万元，不然就扣车，理由是车上有二人证明想要付车费的。证明完那二个人就没了，而其他四个人不肯走，说根本没这回事，坚持要为驾驶员讨个说法。被罚的驾驶员要求执法部门安排面见一次那二位证人，以当面对质，但此要求被拒绝。不得已，驾驶员准备向法院起诉。<br />
<br />
文章见报后没二天，《新闻晨报》又在头版刊登了一篇《莫名“黑车”惹出“放倒钩”疑云》文章。说得是一位姓胡的驾驶员在开车途中遇到二男一女拦车，其中女的是一脸痛苦，象似病得不轻，被两名男子搀扶着。二男子说帮帮忙，就到前面一站路处停下。胡司机勉强同意了。车没开出300米就被拦下，上来几个身穿制服的人，没出示任何证件，但口称自己是交通执法队的。那“病女”上车时一直没开口，现在却开口了，翻脸说自己同意付给驾驶员5元车费的。“病女”之言就是证词，胡司机因此被罚一万元。<br />
<br />
从神秘客人上车到车被拦截，前后一分钟不到。胡司机想不明白，记者也想弄个明白，于是打电话给城市交通执法总队。问这其中是否存在放“倒钩”可能？“倒钩者”是否如人们传说的一次可以得到200元奖励？还有，如何区分“乘客”的话就是真的，驾驶员的话就是假的？再有，作为被罚的驾驶员能否有权与指认他收费的三个证人对质见上一次面吗？<br />
<br />
