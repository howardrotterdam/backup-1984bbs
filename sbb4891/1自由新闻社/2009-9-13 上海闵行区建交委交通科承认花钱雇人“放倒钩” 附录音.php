													<h2>白领好心载客被倒钩 执法部门称谈钱就是黑车</h2>
						
						
																											<div id="postmessage_241293" class="t_msgfont">■ 当事人称自己年入20万，让陌生人搭车是因为其声称“胃疼”&nbsp;&nbsp;■ 遭遇“反扣&nbsp;&nbsp;手臂卡脖子”&nbsp;&nbsp;■ 网上发帖被韩寒博客转载引发热议<br />
<br />
　　“放倒钩抓黑车”执法方式曾引发广泛争议。近日，上海再曝一起极端案例，年入20万的世界500强企业白领发帖称，自己好心让一名声称胃疼的路人搭便车，却被执法部门定性为“非法营运”。不仅如此，这名白领还遭遇了“扭手臂卡脖子”的待遇。<br />
<br />
　　这篇帖子随后被韩寒的博客转载，随即引发网友热议。网友们认为，面对这样的执法方式，以后谁还敢发善心？<br />
<br />
　　东方早报记者 顾文剑 杨静<br />
<br />
　　实习生 张小叶 <br />
<br />
　　<br />
<img src="http://i31.tinypic.com/2zz5mpv.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
<br />
闵行城市交通行政执法大队门前。早报记者 张栋 实习生 朱晓咏 图<br />
<br />
<br />
<img src="http://i25.tinypic.com/9sxufp.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
行政处罚决定书显示，当事人非法运营。<br />
<br />
<br />
<img src="http://i31.tinypic.com/2ec42fl.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
当事人出示的工资单显示其月薪过万。<br />
<br />
　　9月11日，韩寒的博客转载的一篇帖子格外引发关注。帖子中，一名自称是上海外企的白领讲述了自己驾驶私家车好心帮忙却被执法部门当成了“非法运营”。<br />
<br />
　　昨日，闵行区建交委回应说，私家车若有谈价行为即可认定为从事“非法运营”。<br />
<br />
　　韩寒博客曝“倒钩”<br />
<br />
　　在这篇帖子中，作者署名 “善良的被骗”，他称整件事“非常有戏剧性”。<br />
<br />
　　作者称，9月8日下午1点左右，他驾驶牌照为皖Q25605的福特私家车由华宁路向剑川路行驶，但在元江路口等红灯时，一名穿白衣的男子走了过进来，并敲了敲车门。作者描述，那名白衣男子说自己胃痛，因为打不到车，所以请求带他一程，地点是华宁路北松公路转弯处。这名男子还拿出了10元钱当作车费。<br />
<br />
　　作者说，自己先是拒绝，但看到对方“疼痛难忍”随后“心软”，最后同意让这名男子上了车。<br />
<br />
　　在行驶至北松公路转弯处，这名刚刚还“胃疼”的男子在车停驶后突然拔走了车钥匙，七八名身穿制服的人也随即出现“强行”将作者塞进一辆面包车。这几名“制服人员”还开出《闵行区城市交通行政执法大队调查处理通知书》让作者签字，但遭到拒绝。<br />
<br />
　　在韩寒转载这篇帖子之后，该帖的阅读量超过了20万，网友评论4000多条。记者在“爱卡汽车网”的上海论坛中找到了原帖，帖子发表于9月10日晚上11时，署名正是“善良的被骗”。<br />
<br />
　　当事人称“好心办事”<br />
<br />
　　昨天，早报记者找到了当事人张军（化名）。他是圣戈班集团中国磨料磨具公司的市场经理。<br />
<br />
　　张军说，韩寒转载的帖子是自己写的，最初发在“爱卡汽车网”上，内容是自己亲历，可负法律责任。<br />
<br />
　　张军告诉早报记者，每天上下班，他会驾驶自己的福特私家车来回于公司与居所之间，因此会途经华宁路。<br />
<br />
　　在讲述事发状况时张军说，当时他正返回公司，由于那名男子的目的地是往松江方向，张军并未立即同意，但这名男子最后“痛得弯下腰来”，出于“好心”和“善意”，张军同意让他上车。<br />
<br />
　　张军说，就在车子停驶后，那七八名“制服人员”更是不由分说把他从车上拖下来，并反扣他的双手，卡住他的脖子，塞进了一辆面包车里，还拿走了驾驶证和行驶证。<br />
<br />
　　“在面包车上，‘制服人员’拿出一份准备好的《闵行区城市交通行政执法大队调查处理通知书》，并临时写上我的车牌，让我签字，理由是‘非法营运’”。张军没有答应，因为他觉得自己没有任何违法行为。<br />
<br />
&nbsp; &nbsp; 早报记者看到，在张军提供的这份通知书上，签名确认栏上并没有“张军”的名字，取而代之的是“当事人情绪激动，不肯签字”的文字描述。在该通知书上，执法人员一栏上注明的是“黄小峰”。<br />
<br />
&nbsp; &nbsp; 被查扣期间，张军曾要求“制服人员”亮明身份，并出示相关证件。“制服人员”自称是闵行区城市交通执法大队队员，只有一人出示了工作证，却将姓名一栏遮住。<br />
<br />
&nbsp; &nbsp; 在面包车上交涉半小时后，张军被勒令下车，而他的福特车也被“制服人员”开走。“车上的一台笔记本电脑及公司资料，都没能要回来。”张军说，“制服人员”曾表示，等处理完毕，才可以要回车内的物品。<br />
<br />
“我年入20万，<br />
<br />
怎么可能开黑车”<br />
<br />
&nbsp; &nbsp; 对于“非法营运”的定性，张军更认为是无稽之谈。他甚至向记者拿出了工资条，称自己的报酬比开黑车多得多。<br />
<br />
&nbsp; &nbsp; 张军说，他所在的公司是一家外企，自己担任中层管理人员，每天工作十分繁忙，几乎每晚都加班，没时间 “非法营运”。其次，自己年入近20万比开黑车收入多得多。“&nbsp; &nbsp;事发时我正有急事去公司，怎么可能还想着开黑车。”<br />
<br />
&nbsp; &nbsp; 张军出示的这张8月份的工资单上显示，张军的月收入在万元以上，而张军的一名同事王健证实，事发当天，张军原本要返回公司参加一个重要的内部会议。 <br />
<br />
&nbsp; &nbsp; 事后张军认为，上车的那名白衣男子是“倒钩”，并非“寻求帮助”的乘客，而其假装“胃痛”上车，利用了人们的“善良”。“自始至终，我的回答是，我不要你的钱，但只能载你到前面。” <br />
<br />
闵行建交委：<br />
<br />
“谈价”即是“黑车”<br />
<br />
&nbsp; &nbsp; 就帖子中所说的“倒钩”一事，早报记者向闵行区城市建设和交通委员会一相关负责人求证。他很肯定地表示，这事不可能发生，哪怕是协案人员也不会“串通好”，任何一个执法人员都不敢这样做。 <br />
<br />
&nbsp; &nbsp; 闵行区建交委交通科万科长表示，几乎每星期都会接到像张军类似的投诉，但查证后情况均属实，而且闵行区交通行政执法大队的处罚行为没有不当之处。“许多黑车司机都在喊冤，但实际上都在从事非法运营”。<br />
<br />
&nbsp; &nbsp; 张军称自己收入过万，不可能开黑车，对此，万科长强调说，是否 “非法运营”，与当事司机收入高低无关。<br />
<br />
&nbsp; &nbsp; 至于执法部门如何界定“非法营运”，万科长的解释是，只要司机和协案人员谈话内容涉及“谈价”，该私家车可立即被视为“黑车”，处罚依据为“非法营运”，“谈价”的内容包括汽油费、好处费等。<br />
<br />
&nbsp; &nbsp; 万科长称，协案人员在执法过程中有一套严格程序，并掌握了一系列证据，可以充分证明被处罚对象有非法载客行为。<br />
<br />
&nbsp; &nbsp; 当早报记者提出查看证据的要求时，万科长拒绝。理由是这些证据正处在工作流程中，不方便提供。万科长建议，张军可以向法院提起行政诉讼，或者申请行政复议。<br />
<br />
&nbsp; &nbsp; 万科长在最后表示，尚未核实张军所反映的相关事实和具体细节，也因此不排除可能性，即张军的确是 “好心载客”，如果张军反映属实，该部门将予以赔礼道歉。<br />
<br />
当事人无奈交罚款<br />
<br />
&nbsp; &nbsp; 昨日，按照该行政执法大队的处罚流程，张军被“强制性要求”放弃陈诉和申辩的权利。<br />
<br />
&nbsp; &nbsp; 在和窗口工作人员的沟通中，早报记者得知，如果被处罚对象不“放弃上述权利”，那就无法取回车辆。<br />
<br />
