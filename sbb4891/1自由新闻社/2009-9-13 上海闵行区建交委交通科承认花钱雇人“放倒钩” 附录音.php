<br />
放“倒钩”，设“骗局”，抓车主，罚巨款，这事会是真的吗？假如说是真的，那么对于执法部门来说，这将是属于什么性质的问题呢？是滥用职权还是行政违法？或者说是民事侵权？抑或是职业犯罪？<br />
<br />
假如这事不是真的，那么有谁能知道这些以罚代管的执法部门其巨额罚款是如何处置的呢？有谁能监督他们，清楚地知道他们每月罚款收进多少？及时上交国家财政又是多少？是按月上交还是按季或6个月一交？再或者是否有可能先被垫支、调剂、挪用，到年底积少成多地总上交？其后，以财政补贴名义又能返回多少？<br />
<br />
没有一个规范的、能被监督的制度就会滋生种种特权，而特权一旦与专制联姻，就是腐败繁殖的开始。从纳税人腰包里非自觉交来的钱不能做到入账规范、合理、公正、透明，那么，有猫眼是再自然不过的事了。比如说“挪用社保基金大案”，刚被舆论媒体披露时一般人不相信，养老的钱居然还会被人动用？但不可能发生的事偏偏就是发生了。如此，我们凭什么相信，说占用、垫支、挪用罚款的事不可能在执法部门发生？<br />
<br />
有腐败，就有阴暗面，有阴暗面就不可能排除执法部门“倒钩”与“设局”的可能。同样，只要有不透明管理制度的存在，我们就有种种理由怀疑其执法部门可能是因为行政经费发生了问题，或者是经费没计划好，超支严重、出现赤字；再或者是机关里人浮于事，奖金不够分配；更或者是上级下达的罚款指标一直完不成，如此下去极有可能丢乌纱帽。等等。巧妇难为无米之炊，只能靠想尽办法罚款来补贴、平衡。<br />
