													<h2>“钓鱼式执法”置社会道德于何地</h2>
						
						
																											<div id="postmessage_241319" class="t_msgfont">早报特约评论员 傅蔚冈&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;<br />
<br />
　　沪上私家车车主张先生因为动了恻隐之心去救助路边的行人，不料此举竟然引来了“非法营运”的横祸。而更加难以预料的是，在这起“非法营运”案件的处理过程中，还爆出了交通行政执法大队工作人员粗暴执法的花絮。如果这位私家车车主的报料属实，纵使我们的想像力如何丰富，也想像不到交通行政执法部门会通过此种钓鱼式执法来打击黑车行为。（详见《东方早报》9月14日）<br />
<br />
　　城市郊区的黑车问题，是当下中国任何一个城市都无法回避的问题。由于城市化进程的加快导致城市面积的扩大，越来越多的人到城郊生活。同时，过快的城市化导致了不少地方的公共配套和市民需求相脱节的现象：不少地区出现了市民出行难问题，城市郊区的黑车问题于是产生。<br />
<br />
　　城郊黑车的存在，在某种程度上解决了不少地区居民的出行难问题。但是由于黑车不需要缴纳相关税费，与合法营运的车辆产生了不公平的竞争。同时，不少黑车车主也存在着欺诈乘客等现象。因此，无论是从保护合法经营者权益的角度还是保护乘客权利的角度，打击黑车应该是规范城郊道路交通运输秩序中的应有之义。<br />
<br />
　　由于“非法营运”是发生在车主和乘客之间一对一的交易，难免会存在取证困难等问题，交通执法部门也往往苦于证据问题而无法对“非法营运”的车辆进行处罚。为了扭转此种态势，相关部门积极探索新的思路，“改变以往黑车收钱后才认定违法行为的思维定势”，而是通过“运用科技手段先期取证，制作完整的黑车载客营运的视听资料和文字材料证据链，而后择时进行处置，从而使执法部门掌握主动权，降低执法成本和风险”。（人民网2009年7月21日报道）可以预料，涉嫌本次“非法营运”的车主就是通过这样的“科技手段先期取证”而来。<br />
<br />
　　需要指出的是，这种通过“科技先期取证”的做法对于打击“非法营运”有好处，因为它解决了长期以来的取证难问题。但是它的弊病也很明显：因为所谓的“先期取证”，往往是通过交通执法人员假扮乘客而向车主套词的方式获得相关证据，在学理上对以这种方式获得的证据能否作为行政执法的证据尚存争议。同时，在具体的执行过程中，同时还会出现将很多古道热肠的车主纳入到“非法营运”这种大网之中，侵犯相关当事人的合法权益。<br />
<br />
　　根据闵行区建交委交通科相关人员的解释，只要司机和协案人员谈话内容有涉及“谈价”，该私家车立即可被视为“黑车”处理，处罚依据为“非法营运”，而“谈价”的内容则包括了汽油费、好处费等等。这就涉及到对“非法营运”的定性问题。事实上早在2008年，市人大城建环保委就建议有关部门细化“非法营运”的认定标准，将市民之间搭“顺风车”这一类的非经营性活动从“非法经营”行为中剔除。<br />
<br />
　　从常识出发，本案中即便有“谈价”的行为发生，也是属于市民之间搭“顺风车”的非经营性活动，而不是出于以营利为目的的非法经营行为：被帮助者对帮助者的救助行为表示一定的感谢既是善良风俗的要求，也不违背实证法的规定。假如类似的行为都以“非法营运”论处，那么谁还敢在马路边停车救人，更何况车主救人还需要冒着“拦车打劫”的危险？长此以往，在“前不着村后不着店”的荒郊野外，那些急需帮助的人们该从何处获得救助？<br />
<br />
　　黑车之所以存在，是因为市场上存在着出行的需求。为什么城区里的黑车比例要远小于郊区？最根本的原因就在于城区里有着有效的公共交通的供给，不存在“非法营运”车辆的市场。因此，执法部门打击黑车的行为固然有助于打击“非法营运”的问题，但是却解决不了郊区居民出行难这一问题。因此，解决“非法营运”问题的釜底抽薪之计就在于通过降低出租车辆的营运费用和增加合法营运车辆的方式来大力发展公共交通。在居民出行都能够得到保障的情况下，谁愿意去乘坐那些行车安全没有保障的黑车呢？<br />
<br />
　　在现代法治社会中，行政机关的任何行为，都应该是在法律的框架中进行。打击黑车的“非法营运”固然重要，但更为重要的是此种行为需要在法治的框架下进行。如若不然，这种行为既不能够有效地打击“非法营运”行为，同时也有损人民群众的利益和政府的形象，妨碍和谐社会的建立。<br />
<br />
　　（作者系上海金融与法律研究院研究员）</div>

							
							
							
							
