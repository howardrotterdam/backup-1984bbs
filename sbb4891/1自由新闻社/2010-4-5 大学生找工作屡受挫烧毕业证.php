郑克峰拿出其他的证件 记者 董海东 摄<br />
<br />
　　东亚讯 因多次找工作受挫,桦甸籍湖北某学院毕业的郑克峰竟在今年春节前一怒烧掉了毕业证,当时他还把烧证的场景拍成视频传到网上,一度被网友跟帖关注。<br />
<br />
　　2日,记者联系到正在长春找工作的郑克峰,他说来长春半个月了,工作还是没有眉目。面对媒体以及网友关于“烧证”的质询和疑问,他坦陈了内心想法,他说一方面当时有点冲动,同时也想借此引起社会对失业大学生的关注。<br />
<br />
　　自拍自传烧证视频<br />
<br />
　　近日,一段长约1分57秒的“失业大学生烧毕业证”视频疯传网络。视频记录了一个失业大学生烧掉毕业证的过程。视频里大学生本人并未露面,只是把镜头重点放在了火烧毕业证上。<br />
　　“真的假的啊？怎么能烧毕业证呢,那以后找工作怎么办？”不少看过视频的人都很惊讶。同时,也有网友质疑这是炒作。<br />
<br />
　　记者发现上传者是“biyedefannao”,并根据此线索,联系到他本人。“biyedefannao”说他正在长春,真名叫郑克峰,目前处于寻找工作中,已经来到长春半个多月了。随后郑克峰答应可以见记者,谈谈烧毕业证事件。<br />
<br />
　　想用反常举动引人关注<br />
<br />
　　2日17时许,郑克峰如约和记者见面。这是一个外表文秀俊朗的男生,说起上学到毕业的经历,他时而沉默,时而眉头紧锁。郑克峰说,网上视频是他自拍的,烧毕业证也是真的,说着他还拿出了仅有的一些证件,比如英语资格证书、教师资格证书。“也是冲动,也是想说出像我这样（失业）大学生的心声。”他说现在也有些后悔做出烧证的举动,不过现实的残酷让他有些力不从心,他说就业的受挫让自己变得开始自卑。<br />
<br />
　　记者翻看了他的剩余证件,有很多都是别的大学生羡慕的,可现在在郑克峰看来意义都不大。<br />
<br />
　　创业受挫只好摆摊做烧烤<br />
<br />
　　郑克峰说自2005年毕业,一直在广东佛山做代课老师,但只是“顶坑”,每个月1000元左右的工资,他觉得在这个消费昂贵的南方城市,甚至买个西红柿都要1-2块,这么点钱几乎做不成什么？没有钱,只能过着蜗居一样的打工生活。其间获得了教师资格,等级证书,教学设计奖,此外还获得了湖北省政府颁发的“全省理论学习先进个人”,做过汶川地震志愿者。但代课期间,学校一直没有给转正,没有机会,2008年被学校辞退。<br />
<br />
　　“我把最美好的时光献给了教育事业,后来由于身体不好,回到家乡,但创业也没有资本,只能做些小事情。”郑克峰说,为了减轻家里的负担,回到桦甸老家后,白天找学生补习,晚上在学校门口摆小摊,做烧烤,赚几个零花钱,勉强度日。后来又想起考公办老师、考公务员,但都没有结果。<br />
<br />
　　“这次来长春也找了几家单位,去外语学校当老师,但是工资太少了,还有半个月培训期,我现在度日都困难了,根本撑不了那么长时间。”郑克峰说, 现在认为能力还行,只想找份稳定的工作,月薪1000元包吃包住就行。<br />
<br />
　　“我有工作经验,学的是英语教育专业,只想当一名教书育人的老师。”郑克峰说。</div>

							
							
							
							
