该蓝皮书指出，2008年北京城镇居民人均可支配收入24725元，户均可支配收入64285元。其中20%的高收入户人均可支配收入为47110元，户均可支配收入为122486元。20%的低收入户人均可支配收入只有10681元，户均可支配收入27770元。<br />
<br />
而2008年北京住房市场出售商品住宅平均面积110.7平方米，按照2009年13940元/平方米的均价计算，购买这样的一套房需要154万元，相当于一般家庭24年的可支配收入。按照2009年11月的平均价格17810元/平方米计算，购买90平方米的普通商品住房需要支付160万元，相当于一般家庭25年的可支配收入，北京居民的房价收入比为25∶1。<br />
<br />
住房政策的完善是以民生为中心的社会建设的重要基础之一，应该真正把最广大的群众作为政策的惠及对象。该蓝皮书认为，北京的住房政策应该把60%的人纳入保障政策体系，20%的居民（中等收入者及中低收入者）可以享受“两限房”政策，20%的居民（中低收入者）可以租赁公租房或购买经济适用房，20%的居民（低收入者）可以享受经济适用房、公租房和廉租房政策。目前，北京市的政策性住房数量太少，因此，该蓝皮书建议政府部门应该不断完善保障性住房体系，加大力度建设政策性住房。<br />
<br />
如果2010年的保障房建设用地和住房建设计划能够落实，这将是1998年住房改革以来住房政策的一个重大转折。2006年、2007年北京保障性住房比例在住房市场的10%以下，2008年政策性住房有所增加，但是包括“两限房”在内的政策性住房比例也只有20%多，而2009年政策性住房竣工229万平方米，只有住房竣工面积的14%。2010年保障性住房土地供给的面积增加到50%，这意味着北京住房供给将会发生一个结构性变化，也意味着保障性住房政策理念上的转折，还意味着北京的住房保障由特惠模式转向中间模式。如果这个政策真能落实，这将是广大中低收入者的福音。</div>

							
							
							
							
