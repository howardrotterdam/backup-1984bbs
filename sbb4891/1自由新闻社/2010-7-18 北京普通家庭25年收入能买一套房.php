													<h2>北京普通家庭25年收入能买一套房</h2>
						
						
																											<div id="postmessage_746654" class="t_msgfont">　新京报18日&nbsp;&nbsp;北京市居民收入差距几年来呈扩大趋势，20％高收入者可支配收入是20％低收入者可支配收入的4.3倍。<br />
<br />
　　中国社科院和北京工业大学昨日联合发布《2010年北京社会建设分析报告》，该报告称，北京在社会建设方面仍面临高房价等挑战。<br />
<br />
　　高房价加速财产差距<br />
<br />
　　报告指出，2009年北京城乡居民的收入差距达到1∶2.23。行业差距则更为明显，行业间最低月工资收入与最高月工资收入比为1∶8.2。城镇居民中20%的高收入群体人均可支配收入达50816元，而20%低收入群体人均可支配收入仅11729元，前者是后者的4.3倍。<br />
<br />
　　高房价加速了北京居民的财产差距。2009年北京城乡居民收入增加了10%左右，但当年房价猛增73.5%，年初买一套100万元的房子，一年的资产收益达70余万元。有房产者与无房产者的财产距离迅速拉大。<br />
<br />
　　房价收入比达25∶1<br />
<br />
　　报告认为，北京住房价格快速上涨严重超过了一般家庭的支付能力。2008年北京城镇居民人均可支配收入24725元，户均可支配收入64285 元，以一般家庭每户住宅面积90平方米计算，2009年11月北京住房的平均价格为17810元/平方米，需要支付160万元，相当于一般家庭25年的可支配收入，房价收入比为25∶1。<br />
<br />
　　保障性住房不足推高房价<br />
<br />
　　导致房价上涨的最重要原因是保障性住房供应不足。蓝皮书建议，北京应把60%的人纳入保障政策体系，20%的居民（中等收入者及中低收入者）可以享受两限房政策，20%的居民（中低收入者）可以租赁公租房或购买经济适用房，20%的居民（低收入者）可以享受经济适用房、公租房和廉租房政策。</div>

							
							
							
							
