													<h2>《2010年北京社会建设分析报告》：北京中产阶层约540万人多数成房奴车奴</h2>
						
						
																											<div id="postmessage_748190" class="t_msgfont">《2010年北京社会建设分析报告》：北京中产阶层约540万人多数成房奴车奴<br />
<br />
发布日期：2010-07-18 作者：21cnci <br />
<br />
北京工业大学和中国社科院社科文献出版社联合发布的《2010年北京社会建设分析报告》公布的数据称，北京中产阶层在社会阶层结构中所占的比例已经超过40%，约540万人。<br />
<br />
报告从社会事业、社会管理、社会结构三个方面对2009年至2010年北京社会建设中的热点问题进行剖析。<br />
<br />
根据《报告》对北京社会结构的分析，目前北京中产阶层在社会阶层结构中所占的比例已经超过40%，约540万人，这一比例高于23%左右的全国平均水平（据中国社科院社会学所“当代中国社会结构变迁研究”）。这一点与北京作为大都市的经济、文化等优势密切相关。<br />
<br />
从收入状况来看，北京市统计局7月16日公布的2009年北京职工月平均工资为4037元，《报告》调查的中产阶层平均月收入为5923.18元，中产阶层家庭平均月收入为10007.96元（家庭人均月收入为3692.97元），而北京城市居民家庭平均年收入水平22417元。鉴于调查中发现中产阶层低报收入的情况十分普遍，因此，中产阶层实际收入应高于调查结果。<br />
<br />
北京中产阶层收入的内部分化程度要高于社会总体水平。其中最高收入群体是中小企业主群体，月平均收入达到9666.67元，最低的则是办事人员等群体，为2947.76元。调查结果表明，中产阶层20%最低收入组与20%最高收入组的收入差距为9.09倍。<br />
<br />
《报告》指出，当前中国中产阶层主要包括党政机关事业单位中的中层领导干部、中小私营企业主、企业部门经理人员、教师、医生等专业技术人员、办事人员以及部分个体工商户。在中产阶层内部，只有170万人，即30%左右处于中上层，还有近70%处于中下层，面对着工作和生活的双重压力。<br />
<br />
从内部分层来看，处于中产阶层最上层位置的主要是领导干部，此外还包括大部分中小企业主和部分经理人员、专业技术人员。他们接受过良好的教育，掌握一定的权力，拥有高收入，消费能力强，普遍拥有房产、私家车以及其他高档消费品，在文化资源、组织资源和经济资源拥有方面处于优势。<br />
<br />
办事人员、大多数个体工商户以及中初级职称的专业技术人员处于中产阶层的中下层位置。规模大约在308万人左右，占中产阶层总人数的68.5%。他们接受过良好的教育，但在组织资源和经济资源拥有方面处于相对劣势位置。消费需要旺盛，用并不高的收入水平面对高房价、汽车等大宗消费，成为“房奴”、“车奴”。<br />
<br />
中产阶层被冠以“消费前卫”的头衔，在人们的印象中，这一群体住大房子，开好车、生活方式丰富多彩，而实际上中产阶层却由于经济压力过大，社会保障水平不高，倾向于财富的积蓄，消费受到抑制。<br />
<br />
这表现在，基本耐用消费品普及，住房、食品等实物消费在中产阶层家庭消费结构中占有突出的位置，服务消费没有得到充分的成长。他们的消费支出占收入比重远远低于社会平均水平，如家庭月均消费4600元左右，人均消费1700元左右，消费支出不到家庭收入的一半。<br />
<br />
调查显示，中产阶层的负担还很重，尤其那些处于中下位置的中产阶层，教育、医疗及住房消费的巨大压力，正在透支他们的生产能力。因此《报告》指出，减轻中产阶层的负担应是政策设计安排的重要取向之一。这些政策应包括提高个人收入所得税起征点，加强对教育、医疗、住房等民生领域的调控。<br />
<br />
有专家建议参照北京主要消费品价格，如商品房的平均价格，确定个人所得税起征点，这样可以避免个人所得税起征点一刀切，使经济发展水平不同地区得到区别对待，同时也可以使个人所得税的缴纳水平与消费水平相挂钩。对于各个阶层而言，这样做更为公平合理。<br />
<br />
关键词・房价<br />
<br />
一般家庭25年买套90平米房<br />
<br />
内容：北京房价近年来不断上涨，按照2009年11月的平均价格17810元/平米计算，在北京购买一套90平米的普通商品住房需要支付160万元，相当于一般家庭25年的可支配收入，北京居民的房价收入比为25∶1。<br />
<br />
建议：专家指出，北京的住房政策应该把60%的人纳入保障政策体系，20%的居民（中等收入者及中低收入者）可以享受“两限房”政策，20%的居民（中低收入者）可以租赁公租房或购买经适房，20%的居民（低收入者）可以享受经适房、公租房和廉租房政策。目前，北京市的政策性住房数量仍太少，政府应不断完善保障性住房体系，加大力度建设政策性住房。<br />
<br />
根据北京2010年土地供应计划，今年限价房、经适房、廉租房、公租房和定向安置房用地达到1250公顷。<br />
<br />
关键词・养老<br />
<br />
“独子养老”时代需分层管理<br />
<br />
内容：根据预测，到2020年北京市老年人口数量将达到350万；2050年将达到650万的峰值，每3个人中就有一个60岁以上的老年人。“独子养老”时代的到来，传统家庭养老的模式让年轻人不堪重负。北京为此启动试点，在社区建服务站服务老人。<br />
<br />
建议：对老年人群体进行细分和定位，分层次、分类别建设和管理养老机构。具体来说，政府投资建设基础性、保障型养老服务机构，以低收入老年人为服务对象，实行政府定价；政府通过政策扶持社会力量建设一般性、舒适型养老服务机构，以一般工薪老年人为服务对象，实行政府指导价，使多数普通老年人愿意住且能住得起；此外，还应有完全市场化运作的高档型养老服务机构。同时，城六区应该着力发展社区中的小型养老院，充分利用社区内的闲置资源，又避免了修建专门养老机构的高昂成本。<br />
<br />
释疑<br />
<br />
中产阶层≠中等收入<br />
<br />
北京的中产阶层有540万人？对于这一数字一些市民表示怀疑。一直以来，对于中产阶层的特征，人们认为是收入标准。在公众的想象中，中产阶层是那些拥有高收入、消费前卫、生活体面的群体，收入成为中产阶层最重要的身份标识。<br />
<br />
《报告》指出，中产阶层不等于中等收入。中产阶层是以脑力劳动为职业，或拥有一定的生产资料，从事中小规模生产经营，拥有的经济、组织资源处于社会中间位置，而文化资源水平则处于优势位置的人。<br />
<br />
在西方社会，对于中产阶层收入并非如国内如此看重。据美国布鲁金斯学会划分，在美国，家庭年收入在3万美元至15万美元的都是中产阶层，2008年美国家庭税后平均收入水平在4万美元以上，由此美国中产阶层的大多数是中低层收入的群体。<br />
<br />
在西方社会，通常中产阶层对应的是职业群体，但是，在中国中产阶层的“产”更多地被赋意为“财产”。专家指出，在现代社会中，社会分层指标已经从财富维度转向了职业维度，社会分化越来越表现为职业的分化，收入的多寡越来越取决于职业位置的高低。在这个意义上，用职业来界定中产阶层，比用收入更能揭示中产阶层这一群体特质。<br />
<br />
《报告》认为，强调中产阶层在职业基础上的资源拥有的特征，而不仅仅理解为高收入群体，对于政策安排设计有着更为重要的意义。在技术革命及全球化的背景下，国家的兴盛不仅仅取决于出现一个规模扩大的中等收入者群体，更重要的是取决于一支规模壮大了的拥有文化资源与组织资源的中产阶层队伍。<br />
<br />
中产阶层特征及分布<br />
