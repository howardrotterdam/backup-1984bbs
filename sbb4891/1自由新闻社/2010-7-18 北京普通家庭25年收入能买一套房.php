据《新京报》报道，报告从社会事业、社会管理、社会结构3方面，剖析近两年北京社会建设中的热点问题。报告认为，北京住房价格快速上涨严重，超过了一般家庭的支付能力。2008年北京市民人均可纯收入24,725元（人民币。下同），户均纯收入64,285元，以2009年11月北京平均楼价每平方米17,810元计，一个面积90平方米（900多方尺）单位需支付160万元，相当于一般家庭25年的纯收入。报告认为，导致房价上涨的最重要原因是保障性住房供应不足。<br />
<br />
68%中产沦房奴<br />
<br />
《报告》指出，平均月入近6000元的中产阶层，在北京社会阶层结构中所占比例已超过40%，即约540万人，高于23%的全国平均水平，其中有68.5%处于中产阶层的中下地位，用不高的收入来应对高房价等大宗消费，沦为「房奴」和「车奴」。</div>

							
							
							
							
