包括领导干部、大部分中小企业主和部分经理人员、专业技术人员。他们掌握一定权力，拥有高收入，消费能力强，普遍拥有房产、私家车以及其他高档消费品，在文化资源、组织资源和经济资源拥有方面处于优势。<br />
