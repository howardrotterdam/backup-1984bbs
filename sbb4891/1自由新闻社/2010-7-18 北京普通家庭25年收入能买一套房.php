6成京城人口应被纳入住房保障政策体系<br />
--------------------------------------------------------------------------------<br />
2010-07-19 01:45:12千龙网<br />
<br />
千龙网北京7月17日讯（记者 于振华）今天上午，由北京工业大学、社会科学文献出版社联合出版的《社会建设蓝皮书：2010年北京社会建设分析报告》在北京首发。该蓝皮书指出，北京城镇居民按照2009年的房价购买一套90平方米的普通商品住房，需要支付160万元，相当于一般家庭25年的可支配收入。<br />
