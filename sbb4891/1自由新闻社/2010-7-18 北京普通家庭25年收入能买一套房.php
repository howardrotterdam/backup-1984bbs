中 层&nbsp; &nbsp; 19%<br />
<br />
包括经理人员、高级职称专业技术人员中的大多数。他们在工作中拥有一定的支配力，获得相对较高的收入，消费能力较强，拥有房产、私家车以及其他高档消费品的比率较高，但相当多的人有银行借贷。在文化资源、组织资源和经济资源拥有方面处于相对优势的位置。<br />
