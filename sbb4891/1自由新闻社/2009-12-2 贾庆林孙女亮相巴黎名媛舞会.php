<br />
　　受邀少女必须出身名门、才貌兼具，两位中国名媛受关注<br />
<br />
　　11月29日，24位来自12个国家的名媛齐聚巴黎。她们身着品牌设计师量身打造的华服，戴着高级定制的珠宝，露着香肩美背。她们此行是为了参加第19届年度巴黎成人礼舞会，喜庆成年，迈入社交圈。<br />
<br />
　　成人礼舞会每年定于11月在巴黎克利翁酒店举行，这是巴黎最古老奢侈的酒店之一。它的前身为“名门千金成年舞会”。最初只在英国宫廷内部举办。1990年，法国的社交大师Ophelia Renouard重新包装了这个传统而隆重的舞会，佳丽的挑选范围也不再局限于英国皇室内部，而是向全世界名门少女敞开了大门，但挑选规则却越来越严厉。据悉，能获邀参加舞会的名媛，不但要出身名门、聪明、拥有高学历，外形还要漂亮、苗条。而且现在的成人礼舞会已经不再是贵族家庭联姻的场所。<br />
<br />
　　外媒注意到，早在去年2月，Renouard就开始物色今年参加成人礼舞会的名媛。经过数月的邮件邀请、协调、衣服试穿，24位名媛得以在巴黎相聚。<br />
<br />
