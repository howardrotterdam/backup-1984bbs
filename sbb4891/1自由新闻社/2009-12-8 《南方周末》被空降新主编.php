																											<div id="postmessage_372151" class="t_msgfont"><strong>BBC：《南方周末》总编向熹被“降职”</strong><br />
<a href="http://www.bbc.co.uk/zhongwen/simp/china/2009/12/091213_southern_xiangxi.shtml" target="_blank">http://www.bbc.co.uk/zhongwen/si ... thern_xiangxi.shtml</a><br />
<br />
美国总统奥巴马上月访华期间唯一专访他的中国大陆媒体《南方周末》总编向熹据报道被降职。<br />
<br />
