<br />
据路透社发自北京的消息，曾专访奥巴马的《南方周末》总编向熹已被解除职务，其总编职务由一名来自《南方日报》的人选接替。<br />
<br />
向熹在奥巴马访华期间，在美国驻京大使馆专访奥巴马。由于这篇专访没有事先取得中宣部的同意，引起中宣部下令该篇报道不得上网。<br />
<br />
拥有广大阅读群众的《南方周末》在中国报界以敢言著称，长期关注中国社会问题、贪腐与管理疏失等议题，也正因如此获得美国大使馆的推荐取得奥巴马总统的首篇中国平面媒体的专访。<br />
<br />
据悉《南方周末》这篇专访曾获得中国外交部的同意，但没有照会中宣部，引发该部强烈不满。专访的一部分内容虽然在11月19日出版的《南方周末》登出，但其他中国媒体均不敢再转载。<br />
<br />
在撤换掉向熹后，该报的总编一职由较得中宣部信任的《南方日报》的一名总编接替。<br />
