<strong>RFA：《南方周末》前总编向熹遭降职</strong><br />
<a href="http://www.rfa.org/mandarin/yataibaodao/nan-12132009164722.html" target="_blank">http://www.rfa.org/mandarin/yataibaodao/nan-12132009164722.html</a><br />
<br />
2009-12-13<br />
<br />
《南方周末》前总编向熹最近被降职。《南方周末》是美国总统奥巴马上个月访华期间采访过他的唯一的中国大陆媒体。自由亚洲电台记者杨家岱的采访报道<br />
<br />
据香港《联合早报》报道，《南方周末》前总编向熹最近改任“执行主编”。所有就此事作出报道的媒体都认为，这是一种降职。<br />
<br />
但是，向熹为什么被降职？路透社援引消息人士的话说，这“与他对《南方周末》对美国总统奥巴马的采访报道的处理激怒了共产党的审查人员有关。”但是向熹到底是怎样 “处理”对奥巴马的采访报道的？《南方周末》原定的报道稿中有哪些话当局认为是触犯了红线、不应该报道、而《南方周末》却试图报道的？所有这些关键问题，现在都不得而知。但是有一个事实是大家都已经知道的，刊登对奥巴马总统的采访消息的那份报纸的头版和二版都开了天窗。<br />
<br />
旅美权益人士刘念春表示，《南方周末》前总编向熹实在太冤了，因为他在对奥巴马总统的采访报道一事中实际上不可能“处理”什么，因为他所作的一切都是被动的：<br />
<br />
“ 向熹他以前是主编现在降为执行主编，那明显是降级了。降级的原因他自己可能到现在都莫名其妙。因为他实际上在这边是一个完全被动的角色。他从始至终都是被动接受这些的。比如说他采访，采访的话（是）奥巴马提出来的。而且恐怕允许你采访，那是中宣部答应的，提出什么问题也是中宣部给出的，他是在没有任何主动性的情况下就被降了职。这就证明他提出的那些问题让有关当局不高兴了，可能还不是中宣部，比中宣部更高级的那些人感觉不高兴了。那自然问责制嘛，有人就得承担责任。中宣部不想自己承担责任，那肯定就是《南方周末》的主编承担责任了。他承担责任显然是无辜的。在一个被动的情况之下被降职了。从这件事情可以看出来中国没有任何言论自由。”<br />
<br />
美国约克学院教授周泽浩表示，奥巴马总统选择接受《南方周末》的采访，是因为美方觉得奥巴马能够通过《南方周末》传达通过别的渠道不能传达的信息：<br />
<br />
“我觉得（对）《南方周末》，当然他（奥巴马）下面的随从也可能调查过，觉得《南方周末》比北京的那些官僚报纸宽松一些。他可能想通过这种渠道来表达他的一些信息。可以让他提供一些他想宣扬的一些理念，在其他场合他也未必能够讲，他也可以跟国内的批评者或者说民运方面呀，或者说民主方面有诉求的人，他也有个交代。”<br />
 <br />
周教授说，如果向熹遭降职是因为他想刊登的报道稿中有些奥巴马说的话是中方不爱听的，那就越发没有道理了，因为即使《南方周末》提什么样的问题会受到限制，没有人有权规定奥巴马总统在回答问题的时候说什么：<br />
<br />
“采访的一个特点就是话一说开以后就不知道怎么收场，你也不可能跟美国总统画圈圈，是不是？”<br />
<br />
周教授说，中宣部后面就是中共高层，认识这点对分析中宣部在向熹被降职事件中所起的作用也许有帮助：<br />
<br />
“中宣部的地位也是跟着政治需要来的。就是说中央政府如果在某些政策方面就需要中宣布来干涉的话呢，他们也会非常支持你。但是如果中宣部的一些过激的做法如果跟中央的一些主调、政治局的一些主调如果有点偏离的话，中央也可以置中宣部于不顾。就是说中宣部说话有多少份量我看还是要看中宣部上面的老板，管他工资的人让他发挥多少作用。”<br />
<br />
路透社说，所有接受采访的消息人士都因为怕受惩罚而要求隐去他们的真实姓名。<br />
<br />
这是自由亚洲电台记者杨家岱的采访报道。</div>

							
							
							
							
