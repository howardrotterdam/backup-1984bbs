<br />
向熹原来一直担任《南方周末》总编。路透社援引三位没有透露姓名的报社工作人员说，面对来自中宣部的压力，上个星期，向熹被降级担任“执行主编”，新派来的主编是《南方日报》要闻部主任黄灿。<br />
<br />
据认为，《南方日报》相对来说更受当局信任。<br />
<br />
互联网上也出现了很多关于向熹被降职的报道和评论。<br />
<br />
路透社称，向熹被降职，有可能再次让美国考虑奥巴马访华的影响。事件凸现了华盛顿以及北京就奥巴马访问的新闻审查、报道自由等存在的争议。<br />
<br />
《南方周末》以敢言在中国受到许多读者欢迎。奥巴马不象其他到访中国的领导人，没有选择中国官方电视台接受专访。<br />
<br />
有报道称，中宣部对《南方周末》未事先报批大为不满。<br />
<br />
奥巴马的采访是由白宫提出的，得到了中国外交部的批准后，直接与《南方周末》联系。<br />
<br />
但是，当中宣部得知后，对《南方周末》大加批评，严厉限制了采访者所提问题，并且在交印前几个小时对采访记录大加删减。<br />
<br />
删减之后的采访在11月19日发表。评论人士说，此后《南方周末》故意“开天窗”以示抗议。<br />
<br />
=============================================================<br />
<br />
<strong>德国之声：《南方周末》主编向熹专访奥巴马后被降职</strong><br />
<a href="http://www.dw-world.de/dw/article/0" target="_blank">http://www.dw-world.de/dw/article/0</a>,,5010056,00.html<br />
 <br />
<i>中国《南方周末》主编向熹在独家专访11月访华的美国总统奥巴马之后被降职。此前，11月19日的《南方周末》仅刊登了十分简短的奥巴马专访，而在该期头版和二版各开了半个版面的天窗。一些媒体人和读者认为，这是该报抗议相关采访和报道遭受新闻审查的做法。</i><br />
 <br />
<br />
据路透社报道，《南方周末》三名工作人员在要求隐去姓名的条件下证实，在11月中旬采访奥巴马之后，由于受到中宣部的压力，主编向熹日前被降职为所谓&quot;执行主编&quot;。有消息称，新任主编为南方日报社委、要闻部主任黄灿。<br />
<br />
中国知名博客作者、媒体评论员安替对路透社表示，中宣部肯定对采访奥巴马事件很不高兴。把向熹从原来的一把手降为事实上的二把手，可能是为了避免受到上面进一步的压力。<br />
<br />
作为中国最受欢迎、最具批评性的报纸之一，《南方周末》经常针对社会问题和官员腐败等现象发表调查性报道。尽管像所有中国媒体一样，《南方周末》也受到国家审查制度的监控，但广东省与其它地区相比较为宽松。<br />
<br />
美国总统奥巴马此次访华期间没有选择受到严格审查控制的中央电视台接受专访，而是点名要求与《南方周末》进行采访。据路透社报道，白宫提出的这一要求得到中国外交部的批准后直接传达到《南方周末》，该报即开始准备采访事宜。然而，中宣部得知这一消息后，对该报作出批评，并对专访中提出的问题作出十分严格的限制，其后更是在相关报道应见报前的数小时下令禁止刊登。<br />
<br />
因此，11月19日出版的《南方周末》仅刊登了非常简短的奥巴马专访，而在头版和二版均留出半个版面的空白。在开天窗的地方，仅登出了该报的宣传标语&quot;在这里读懂中国&quot;。其中头版写的是&quot;不是每一期都有独家专访，但是每一周都可以在这里读懂中国&quot;。二版则写着：&quot;不是每个人都可以成为大人物，但是每一个人都可以在这里读懂中国&quot;。一些读者从这种&quot;开天窗&quot;的做法中看出新闻审查的踪迹。一位媒体人介绍说，开天窗的传统在1949年以前的中国就存在。当时中国共产党控制下的媒体以及其它反对国民党统治的媒体用开天窗的方式抗议新闻审查制度。在中国共产党建政后，这样的事件也零星发生过。<br />
<br />
这位媒体人还向路透社透露，中宣部还要求《南方周末》不得刊登奥巴马写给该报的一张字条，上面强调了新闻自由的重要性。另据博讯报道，中宣部还下令各其它媒体不得转载《南方周末》采访奥巴马的内容。<br />
<br />
有分析认为，本次向熹降职事件，将再次引发有关奥巴马访华影响的讨论。奥巴马访华期间，与中国国家主席胡锦涛举行的联合记者招待会没有允许记者提问。而奥巴马在上海与中国大学生的对话，也仅在上海本地的电视台直播。<br />
