&nbsp; &nbsp; 任何报纸都有她的生命周期。南方周末红了十几年，对中国的报纸已经是奇迹了。今年 全国广告爆跌，广东媒体更是惨不忍睹。南方周末虽然有足够财力，维持原有薪酬标准，问题是 她有赡养父母责任，而且要求发展--如城市版，要大量投入。这就为她布置了重重障碍。向熹一 手策划的城市版，定位不准，格调与南方周末读者期望距离很大，也为他的被攻击制造了口实。向熹理应明白，他不是沈颢，不是程益中，也不是刘洲伟，即便龚小跃，李戎，连清川，也胜他 好几分。一位南方周末的大姐说，以向熹的判断力，写作采访能力，如果参加南报公开招考，十有八九不会被录用，他如何能让南方周末的记者心服呢？她评价向氏用了“味道不对”四个字。如果用GOOGLE搜向熹的文章，难有一篇符合南方周末的标准。旧闻<br />
