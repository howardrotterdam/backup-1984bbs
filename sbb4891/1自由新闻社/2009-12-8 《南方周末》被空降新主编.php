<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;天下没有不散的宴席，在一周前珠海开了全体会议后，因对新任主编，南方集团派来的向熹独裁作风不满，南方周末十名左右资深记者向报社递交辞职报告。&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;这些记者是南方周末辉煌时代仅存的一批人马，知情者相信，随他们的出走，饱受弹压，苦难深重的南方周末，将沦为一张普通的小报。一位辞职者说：“我们是雅典的公民，但现在是斯 巴达人的臣民。”&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;南方周末城市版主编陈菊红，头版编辑方三文，消费广场主编曹西洪已经递交辞职报告，记 者李玉宵，杨海鹏，翟明磊，贾明，朱强，杜卫东，甄茜，江华，贾明，周浩已经明确离职意 向。&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;都是向熹惹的祸。让一个在体制外毫无经验的干部，负责一个体制外的媒体，这是南方日报集团 领导用人的失策。向熹此人纸上谈兵可以，但为人浮夸，不够坦诚，南方周末的城市版投入大， 收益小，挤压原版面的广告资源，是典型的样板工程。向熹曾在三年前欲到南方周末当记者，为 江艺平所拒，理由是向熹与南报记者气质上有差异。南方日报的老人都会记得几年前的“七一晚 会”，向熹朗诵一歌颂党的诗，未罢居然泣不成声--给人以大伪之徒的感觉。他那一套在体制内 行得通，但在南方周末行不通。除非对南报彻底换血。问题是南报彻底换了血，魂魄将不复存 在。&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;南报的内部冲突，实际上是体制内外两种文化的冲突。这次事件中，率先倒戈一击的是 上海记者站的两个记者，杨海鹏和翟明垒，他们批评南报新任领导在内部事务中起码的知情权， 居然对记者降薪一半这种大事，事先不沟通，事后不解释。在上海站对此质询时，也不予理会-- 按党报传统，这种事无须多解释；但按南方周末的传统，碰到如此大事，领导层会对大家说明， 寻求谅解。向熹年纪虽轻，但一直深受体制内文化的浸润，自然不知南报的“理路”，把权力的 分量看得太重，而轻视下层的反应。在翟杨公开批评后，南报资深编辑曹西弘深受刺激，此人一向在周末有清誉，在能力操守上无可指责，他的行动是上书南方日报老总，要求撤换能力及理念 无法与周末契合的向熹。但南方日报处理此事者，主观臆断曹是“非组织活动”，要求曹及附议 的翟明垒，杨海鹏道歉，遂使人们对南方日报彻底绝望。询问他们的人揣测，他们是想把向熹拉下马，背后有《南方周末》前领导的主使。但事实上，他们没有证据，甚至在三人（翟杨的信是 给曹的私信）的信中，观点也有很大差异，罗织罪名非常困难。何况以私人信件治人处分，于理 于法不通，遂使向熹步入僵局。处分难，大家会不服；不处分也难--领导威信自然扫地，他的强权会被瓦解。但让他道歉，以向熹的自负，他是不能错的。于是，因为此三人（俱为记者中的憨货）而拉开的伤口，就不会愈合，南方周末就只有失血了。对南方日报来说，调换向熹恐怕是最 得人心的选择。向熹与南方周末的记者，本不是同类，硬凑在一起，冲突不可避免。即便当局 者，也不会想到记者们的反应会这么快--如不及时处置，南方周末几个月精华尽去，不是什么假 设。&nbsp; &nbsp;<br />
