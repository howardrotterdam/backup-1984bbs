据报道，目前正在日本窜访的达赖即将于11月8日访问中印争议领土。10月31日在东京举行的记者招待会上，达赖说：“中国的抗议令我吃惊，因为1962年时，中国人民解放军已经进入并占领了（达旺），后来印度将他们赶了回去，中国政府最终停火并撤军，因此我对那里的访问有什么问题吗？”。据印度媒体报道，达赖当天在记者面前回顾了中印1962年边界战争，宣称“阿鲁纳恰尔邦”的大部分地区战前属于印度，是被中国人民解放军占领的。达赖还谈到了他对“阿鲁纳恰尔邦”深厚的感情，1959年他逃离西藏时，就是从这一地区进入印度的。<br />
<br />
中国藏学研究中心当代研究所研究员刘洪记在接受《环球时报》采访时说，今年上半年，达赖曾表示想加入印度国籍，做一个真正的印度人。如今，他又要去被印度非法占领的藏南地区，那里有六世达赖的出生地（达旺），“这等于把自己的祖宗都卖到印度去了”。刘洪记说，1914年西姆拉会议以后，无论是中国政府还是西藏地方政府在以后的历史中从来没有承认过“麦克马洪线”以南地区属于印度。达赖自己在1959年以前也没有承认过藏南地区属于印度。<br />
<br />
