环球时报11月3日报道&nbsp;&nbsp;“为了为‘藏独’势力捞取生存空间，达赖想把中国9万平方公里的土地都出卖给印度，他算得上是当今中国最大卖国贼了。”对于达赖最近在日本放厥词称藏南地区（印度声称的阿鲁纳恰尔邦）属于印度的言论，环球网友做出了这样的评价。在环球网所做的话题调查中，九成受调查网民对达赖这种赤裸裸的卖国行径感到气愤，有网友调侃达赖“现在已经里外不是人了，所以无关乎自己的血肉之躯来自于哪”。<br />
