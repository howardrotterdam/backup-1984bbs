2008-09-25 13:34 <br />
07年11月28日 星期三<br />
南京师范大学副教授郭泉发表公开信建议采多党派民主体制 (附公开信全文)<br />
<br />
　　<br />
尊敬的国家主席胡锦涛、全国人民代表大会常务委员会委员长吴邦国：二位领袖，<br />
您们好！<br />
<br />
我是中国民主同盟盟员郭泉。中共的十七大会议确立了“坚持以人为本”的“科学<br />
发展观”和“构建和谐社会”的执政理念。本人就“科学发展”、“和谐社会”两<br />
个主要的方面，写此信与两位领袖探讨建立“全民福利条件下多党竞选的民主政体<br />
”的可能性。<br />
<br />
1949年以后，中共的历届领袖和各级领导，尽管犯过各种大大小小的错误，延误了<br />
中国各方面的发展，但是我们还是应该肯定中共的一切政令的初衷都是为了人民的<br />
幸福和国家的富强的，对此我不持疑义。<br />
<br />
但是，在一党是否能治理好中国的问题上，我与贵党持不同政见。虽然，贵党和我<br />
盟以及其他各民主党“肝胆相照、荣辱与共”，但是“参政”和“执政”是完全不<br />
同的两个概念。两者的区别不只是位置的问题，而是一种源自对国家对民族负责的<br />
历史使命感，和源于对人民手中的选票的畏惧。这种对选票的畏惧和历史使命感，<br />
才是为人民服务的真正保证。<br />
<br />
以下就十八个方面，与两位领袖探讨“全民福利条件下多党竞选的民主政体”。<br />
<br />
一、什么才是社会主义？<br />
<br />
全民福利的社会，就是社会主义社会。整个人类社会，在经济上就是一个从剩余价<br />
值垄断到剩余价值共享的一个过程。<br />
<br />
在早期资本主义社会里，剩余价值被其中一个占统治地位的统治阶级（资产阶级）<br />
垄断，在当今发达国家的资本主义社会里，剩余价值正在从垄断的一端走向均衡。<br />
北欧一些国家的这种趋向均衡的迹象特别明显。<br />
<br />
彻底的、完全的均衡就是共产主义社会。大多数的剩余价值作为全民福利分给人民<br />
的社会，就是社会主义社会。所以社会主义的特征是全民福利。<br />
<br />
马克思根据社会财富的所有制和剩余价值的分配情况，认为在经历过高度发达的资<br />
本主义社会之后，人类将进入共产主义社会。而共产主义社会的两大特征，就是物<br />
质极大丰富，人性极大发挥。<br />
<br />
资本主义结束后，由于人们在物质生活和人性认知两个方面，都还没有完全达到共<br />
产主义社会的要求，于是必须有一个共产主义的初级阶段，即社会主义社会。因此<br />
，社会主义社会的主要特征也应该是在物质和人性方面具有很明显的超越资本主义<br />
的特征。<br />
<br />
社会主义，是共产主义的初级阶段，分配方式是在兼顾公平的原则下的按劳分配，<br />
至少要达到“使老有所终，壮有所用，幼有所长，鳏寡孤独废疾者，皆有所养。”<br />
（《礼记》）<br />
<br />
目前的资本主义国家，正在按照马克思设想的路线逐步走向社会主义。他们在接受<br />
了多次经济危机和人道主义危机（对反抗人民的镇压）之后，资本主义国家早已开<br />
始把剩余价值的一部份以国家福利和“股份”的方式分配给了国民，使得资本主义<br />
国家人民的社会福利方面，要比目前的中国优越很多。另外，资本主义国家不仅在<br />
财富上实行了相对公平的分配和保障，他们还把执政权利拿出来供社会各界群众进<br />
行竞选获得。尽管各党都有各自的利益追求，但是从他们的竞选中，获利的是人民<br />
。<br />
<br />
这一切使得资本主义国家的人性得到了发挥，其具体表现，就是在社会财富的获取<br />
方面实现了一定程度的分配公平，在政治实施方面实现了一定程度的竞选公平。<br />
<br />
很显然，资本主义的特点是财产私有、高福利和多党竞选。那么，社会主义就是在<br />
这三点上的全面完善和改进，使财产所有权更明晰、剩余价值更多地分配给人民、<br />
人民能组织人民的政党进行多党竞选。<br />
<br />
二、什么是社会主义民主制度？<br />
<br />
全民福利条件下的多党竞选的民主制度，才是社会主义民主制度。<br />
<br />
目前，很多人无论是政治专业的，还是非政治专业的，一提到政治，他们立即联想<br />
到“资本主义”和“社会主义”。其实，“资本主义”和“社会主义”都只是社会<br />
财富的管理方式而已，是经济制度，而不是政治制度。<br />
<br />
而政治制度是指代表各人民群体的社会不同思想及其党派的“准入制度”。准许允<br />
许不同思想及其党派进入的政治制度才是民主政治，民主政治下，人民有权选择不<br />
同的思想及其党派在一定期限内成为执政党。而专制政治，却只允许本党是执政党<br />
，其余一切政党只是参政议政党。<br />
<br />
经济制度，分为民主制度下的经济制度和专制制度下的经济制度。民主制度下的经<br />
济制度，有资本主义经济和社会主义经济两种。专制制度下的经济制度，有奴隶主<br />
义经济和封建主义经济。<br />
<br />
社会主义民主制度就是在产权明晰和剩余价值更多地分配给人民的经济前提下，允<br />
许代表各阶层的人民组建各自的政党进行多党竞选而获得执政地位的政治制度。<br />
<br />
事实上，民主无论目的还是方式，都是最最简单的人类行为――“选择”而已。“<br />
选择”是人类生活中最简单、最平常的事情。例如，我们为了生存下去，必须解决<br />
每日的饮食。我们在菜场购买食物，手里拿着钞票，眼里看着多种多样的蔬菜或肉<br />
品。经过一番比较之后，作出购买行为。这种“持币选择”的过程，难道不是民主<br />
的持票选择执政党的生活影像吗？<br />
<br />
再例如，我们为了种族和家族的繁衍，我们必须谈婚论嫁。娶哪位姑娘，嫁哪位郎<br />
，这都是一种选择行为。还有我们考学，无论是入托、幼升小、小升初、中考、高<br />
考、考研、考博、进博士后流动站，我们无时无刻不在选择。“选择”是我们每日<br />
最大量的行为方式。只要是人，那么我们就时刻在进行着对自己有利的选择。任何<br />
人、任何群体的选择都是“趋利避祸”的。<br />
<br />
执政党的优劣，将影响到全体人民的根本利益，所以，人民对执政党的选择必然也<br />
是最慎重的。所以，民主除了政治范畴的意义外，其实也是我们最日常的一种生活<br />
方式。<br />
<br />
全民福利条件下多党竞选的民主政体，就像一个引擎，它用人民手里的选票产生出<br />
一个必须为民众服务的执政党。民主有一只看得见的手，一只最强有力的手，这就<br />
是“选票”。<br />
<br />
选票，动态地均衡着各个阶层、政党、群体、道德、利益集团的关系，甚至约束着<br />
政治领袖的行事规范。<br />
<br />
三、两党政治（执政党和在野党）的民主理论不是西方的专利。<br />
<br />
执政党和在野党竞选轮替的民主理论不是西方的专利，其理论基础是我们中国的传<br />
统文化中的阴阳学说。现在有一种说法，两党理论是西方的东西，而中国一直是有<br />
中国国情的，不适用西方的两党政治。其言下之意，就是集权专制才符合中国国情<br />
，才是应该受到中国人民顶礼膜拜的唯一统治制度。<br />
<br />
就统治而言，一党只沟通一部份公众，除了枪炮外，又没有其余足够的渠道，这样<br />
最终的结果是，执政党沟通不了全部的公众，其他民主党派又由于是友党或投票举<br />
手党而没有实际存在意义，这样就造成了公众和执政党的诸多对立，中间许多公权<br />
沟通不起来，没有足够繁荣的利益沟通机制。<br />
<br />
如果有执政党（规定年限执政），同时还有一个或多个等待被选举执政的在野党，<br />
就能比较好地解决这个问题。同时，两党轮流执政，最大程度提高了在野党的监督<br />
能力，最低限度降低了社会动荡，杜绝了以战争形式进行政权更迭给人民带来的巨<br />
大痛苦。<br />
<br />
虽然，这个美丽的计划是由西方人最先实施起来的，但是，这个美丽的计划却是我<br />
们中国传统文化中的阴阳理论奠基的。有阳有阴、“阴阳交相胜”、阴阳才能达到<br />
均衡。所以，一切只有执政党（阳）而没有在野党（阴）的情况，就是只有“阳”<br />
的国度、就是不合“阴阳”之道的国家。<br />
<br />
可惜的是，秦灭六国，统一天下后，中国传统文化的这一伟大的思想，立即被强大<br />
的专制集权界定为异端思想。历朝历代，中国只有执政党，没有在野党。从此，政<br />
权更迭只有靠武装颠覆，除此之外，别无他法。<br />
<br />
现在，我们不能因为“千百年来秦政而已”，就认为两党（阴阳）思想是外来的、<br />
西方的思想。我们更不能因为中国传统文化的阴阳理论在西方生根发芽开花结出丰<br />
硕的果实，就拒绝认为两党民主思想奠基于我们中国。<br />
<br />
因此，两党理论不仅是西方的，也是东方的。它属于人类最优秀的文化遗产。<br />
<br />
四、两党政治是社会和谐的最有效途径<br />
<br />
社会和谐的根本标志就是社会各阶级的意见能充分交流，最后以投票的方式决定谁<br />
向谁妥协。如落败的一方有意见，那么就必须重新调整方案，争取新一轮的多数票<br />
。<br />
<br />
研究社会和谐以及促进社会和谐的条件，必须把重点放在分岐和共识的根源上。必<br />
须认识到，冲突与共识在一定的条件下是可以而且必须保持平衡的。但是这种平衡<br />
，不是靠单方面的宣布，或一方对另一方在言论和身体上的杀戮来实现的。<br />
<br />
一个国家社会里，无论执政者怎么努力，都是无法消除一切的不公正现象的。各种<br />
分歧每日都在产生，其最终形态其实就是人民的满意程度与执政者施政能力之间的<br />
矛盾。<br />
<br />
于是，就有一些在野的知识份子自觉或不自觉地成为人民声音的代言人。专制国家<br />
，是不允许他们出现的，于是就有杀戮，于是就叫专制主义。民主国家里，他们组<br />
成的集团就是在野党。在野党和执政党，从一开始的任务就是力争代言大多数的民<br />
众，这样就能在下一轮竞选中获得多数票，其年限是连选连任不得超过8年。<br />
<br />
稳定的社会要求冲突或分岐的具体化，以致出现围绕统治地位的斗争、对执政党的<br />
挑战和执政党的更替，但是，如果没有共识，即没有一种允许对政权进行和平“博<br />
弈”，允许“在野党”批评“执政党”的决策，允许“执政党”承认“在野党”的<br />
权利的政治系统，就不可能有社会和谐。<br />
<br />
威权主义国家由于只有一个权力中心――执政党，而没有其他的团体足以与之抗衡<br />
；由于限制民众组党、限制民众办报，所以民众的利益就无法避免遭受来自单方面<br />
的损害。<br />
<br />
这样的政治单方面掩耳盗铃式的宣称的社会和谐，不仅是令人绝望的，而且是没有<br />
意义的。<br />
<br />
五、只有当人民手里真正握有选票的时候，执政党才可能是真正的公仆<br />
<br />
公仆意识的获得不是因为政客们的思想觉悟高。古罗马的人民都知道一个道理，其<br />
实政客们争相为人民服务的原因，不是他们的良心发现，而是政客们想要人民手里<br />
的选票。<br />
<br />
古罗马是一个共和国，他们在中国的孔子时代就发明出了民主的政治体制了。共和<br />
国国民每人手上都有选票。人民有选票，就会有想参政的富人来求你将选票投与他<br />
，他就得为穷人办事，办实事，办得穷人心花都怒放了，选票就给你了，你就可以<br />
拿着穷人选票，做执政官，施展你的政治抱负。人民有选票，那些还想继续做政客<br />
的政客们就必须在任期间认真做公仆。否则，人民的选票就不会继续投给他。这就<br />
是古罗马的伟大！<br />
<br />
什么人愿意为人民办事情，做人民的公仆？人类历史上只有两类人在为穷人办事，<br />
甘心做人民的公仆：<br />
一是宗教教徒，他们为了自己的信仰，救苦救难，通过救助贫困者，扩大信仰者，<br />
实现自己对神的承诺或获得心灵的提升。例如：佛教的普度众生，基督教的荣神益<br />
人。<br />
<br />
二是需要选票的政治家和他们的政党以及以他们的政党为执政党的政府，他们通过<br />
为广大穷人办事，获得选民们的支持，以赢取选票，为自己参政和行政开道。<br />
<br />
来自宗教的福祉并不能彻底解决我们的日常生活中的大量事务，与我们日常生活密<br />
切相关的是行政组织。<br />
<br />
但是，这些行政组织为什么会为人民服务？这种为人民服务的公仆意识，仅仅依赖<br />
伟大的思想觉悟是根本无法得到保障的。<br />
<br />
公仆意识到底与谁有关？公仆意识到底与什么有关？答案只有一个，公仆意识与人<br />
民手里的选票有关。如果人民手里没有选票，或选票无能，那么执政官就不会是一<br />
个公仆，而一定是一个皇帝或一方诸侯。<br />
没有民主体制，人民手里没有真正意义上的选票，无论行政的优劣都始终是唯一的<br />
执政党，这就是中国所有问题的症结所在。<br />
<br />
六、中国的政体必须尽快改为权力来源于人民的、法制公平的宪政国家<br />
<br />
我多年研究1949年后的中国政体，发现不只是我一个人对这个政体不满意，在我之<br />
前的，以及在我身边的许多人也都不满意。如果仅仅是不满意，还不算什么，更严重的是，这个政体的很多创建者，后来既是这个政体的守护者和责任者，也是这个<br />
政体的受害者。<br />
<br />
每当我看到这个政体下的受害者的史实，其受害范围从黎民百姓到国家主席，我就<br />
在想，我们国家的政体叫中华人民共和国，但共和国最本质，最核心的东西是什么<br />
呢？应该是民主和法制。而这个叫中华人民共和国的政体，所缺的恰恰是民主和法<br />
制。<br />
<br />
解决的方法其实是存在的，只是执政党怕自己的统治地位不保而不愿意采用罢了。<br />
这个办法就是向美国宪法学习。美国成为超一流强国，靠的就是这个东西，中国要<br />
成为一流国家也得靠这个东西。<br />
<br />
向美国学习，应该理直气壮。比美国差，就应该承认我们的不足，特别是在民主宪<br />
政方面。回顾一百年来，对中国欺负最少的大国就是美国了。退回庚子赔款，让中<br />
国人到美国留学不说，八年抗战，美国的援助比苏联援助多得多！抗美援朝与美国<br />
打仗，是金日成和斯大林加给中国的。美国是第一强国，中国的发展和统一都绕不<br />
开美国。世界和平与发展也离不开美国。<br />
<br />
中国的政治改革必须向美国的宪政学习，中国的政体必须尽快建成为权力来源于人<br />
民的、法制公平的宪政国家，这是孙中山先生的梦想，也是我们当代人的责任。<br />
<br />
七、中国的领导人还是得靠人民来选，不能再靠枪杆子和小圈子<br />
<br />
长期以来，中国是一个封建制国家，中国历史上的改朝换代，无一例外都是通过战<br />
争来实现朝代的更迭的，这是所有封建王朝的本质特点。任何一个王朝，哪怕再不<br />
会治理国家，都不愿意把江山拱手让人。于是代表新思想的力量只能通过武装力量<br />
来获取政权，实现朝代的更迭。<br />
<br />
孙中山先生领导的中国国民党发动的辛亥革命，推翻了满清皇帝的家天下。孙中山<br />
在革命成功后，设计了“军政”、“训政”、“宪政”的三步走的三政革命路线。<br />
<br />
<br />
北阀战争后，结束军政统治，百废待兴，国民党按部就班地实行“训政”。然而，<br />
就在一切欣欣向荣的时候，日本入侵中国，打掉了中国经济发展的势头。国民党被<br />
迫重新实行“军政”，直到1945年战争结束。<br />
<br />
二战结束后，国民党立即从“军政”转向“训政”。而此时的共产党和中国民主政<br />
团同盟等民主党，纷纷要求国民党实现“宪政”，即多党竞选的民主政治。在这种<br />
情况下，国民党先是同意“宪政”，后又认为 “宪政”条件并不成熟。于是中日<br />
战争后，开始了内战。最后，共产党取得胜利。<br />
<br />
49年，共产党取得政权之后没有人提到“宪政”的问题。1957年，民盟中央的罗隆<br />
基、章伯钧、储安平等领袖曾在共产党“党政”八年之际要求实行“宪政”，结果<br />
却被共产党打成“右派”，更多的民主人士被送入牢狱，迫害致死。从此，民主党<br />
派噤若寒蝉，“宪政”无人敢提。<br />
<br />
当然，枪杆子和小圈子，有其历史的原因，但是，21世纪的中国，显然不能再继续<br />
这种家天下和党天下的专制制度了。今后，中国再也不能枪杆子里面出政权了，也<br />
不能靠枪杆子维持政权。执政党是靠老百姓的税收来维持生活的，你要老百姓养你<br />
，你就得代表民意和服从民意。<br />
<br />
古语说，得民心者得天下。我看，未来的国家领导人，应该靠实事求是的本领，靠<br />
真理和民心民意来接受人民的选择，来维持和完善政权。<br />
<br />
八、民主制，作为人类最文明的科学管理体制才真正是三个代表<br />
<br />
当前，困扰中国的一个很大的问题，就是一提到民主制度，马上人们就把她理解为<br />
一种政治制度。其实，民主制是人类最文明的科学管理方法，是人类共同的精神和<br />
物质财富。无论对经济生活，还是政治生活来说，民主制度都是人类生活的最好的<br />
管理体系。<br />
<br />
首先，我们来看看生产力方面，民主制会选择最有效的生产方式，使用最有效的生<br />
产工具，促进最环保的生态环境。任何专制在生产力面前都无法实行如此美妙的技<br />
术控制。没有民主和法制做保证的市场经济是根本无法维持下去的，只会是黑幕与<br />
腐败的温床。所以，民主制代表了最先进的生产力。<br />
<br />
其次，在文化方面，民主制必然选择人民最喜爱的文化内容和文化形式，一切反人<br />
类的文化，都会遭到唾弃。所以，民主制代表了最先进的文化。<br />
<br />
第三，在最广大人民的根本利益方面，民主制可以在多党里实行筛选，选择能让人<br />
民的利益最大化的政党来为人民执政。如果不合适，人民也有权罢免执政党的执政<br />
权力，重新选择更好的政党来为人民执政。<br />
<br />
民主制还可以消除人民对执政者的情绪化思维。目前在中国到处弥漫着一种人民对<br />
执政党不满而无法改变的情绪化思维。国家领导人和议员，如不代表人民说话，人<br />
民就不选你。人民手里的选票一定是投给那些真正能代表人民利益的政党及其领袖<br />
的。所以，民主制代表了最广大人民的最根本的利益。<br />
<br />
民主制，不是革命口号，而是人类社会的最伟大的实践活动。民主制，代表着最优<br />
秀生产力，民主制也代表着最先进文化，民主制更代表了最广大人民的最根本利益<br />
。<br />
<br />
九、关于经济和政治的关系，我认为应是“改革开放加民主政体”<br />
<br />
孙中山先生的三民主义，目前只实现了一个半。实现的一个是“民族”，实现了半<br />
个的是“民生”，还有一个还没有实现的是“民权”。民生问题，其实就是经济问<br />
题，而民权问题，其实就是政治问题。<br />
<br />
孙先生时代的“民族”问题已得到彻底解决，靠的是辛亥革命及其以后的诸多针对<br />
满清的武装革命。当前中国，解决“民生”问题，靠的是改革开放。虽然民生问题<br />
还有很多问题，但是毕竟我们现在的人民生活要比以前好了，路上也没有随处可见<br />
的饿死冻死的黎民百姓了。说“民生”只解决了一半，是因为其中存在的问题也是<br />
巨大的，例如，贫富分化过于悬殊。一部份利用各种合法或不合法的手段先富起来<br />
的人，根本不想带动人民共同富裕。人民的生活本该更好些的，但是，由于大量的<br />
财富被先富者攫取和霸占，随着厂房、医院、学校、银行被大量出售，人民的生活<br />
水平已经不再有可能进一步提高了。但是，人民的民主意识提高了。<br />
<br />
现在，中国人民有了一种普遍的觉醒的认识，即对社会制度的不合理与不公平、对<br />
理想变成荒唐、幸福变成痛苦的日益觉醒的认识，这种觉醒的认识，标志着“民权<br />
”时代即将到来。<br />
<br />
经济和政治，是社会的两大最基本元素。政治是为经济服务的，这种服务不只是促<br />
进经济的发展，也还行使了控制经济发展的道德方向的权力。在经济和政治的两者<br />
关系上，当代中国，存在着三种思维的差别。<br />
<br />
一是以“经政皆左”思想，他们提出经济上使用“鸟笼”经济，反对改革开放，政<br />
治上他们主张回到高压专制时代。<br />
<br />
二是以“经右政左”思想，他们认为经济上应该坚持改革开放，但是政治上主张“<br />
温和的专制”，即不打击民主思想，但是捍卫一党专政。<br />
<br />
在这里，我要提出我的“改革开放加民主政体”的思想。我认为，必须坚持改革开<br />
放，这是中国能快速追上世界发展潮流的基本前提。同时，在政治上，我主张人民<br />
有权根据各自的管理思想组建政党，人民有权选择执政党，并实行多党竞选的民主<br />
政体，第一种思想无视当代世界的民主潮流。第二种思想无视“大跃进”、“文革<br />
” 给中国人民带来的深重灾难，他们还忽视了在单一政治的高压下，最自由的经<br />
济体制都是难有所作为的基本事实。<br />
<br />
而我的看法是，凡是既搞市场经济又搞民主政治的国家都会繁荣富强、社会稳定，<br />
例如英美法德意日。凡是既实行市场经济又搞专制的国家，都走向失败，成为腐败<br />
的市场经济，引发社会矛盾动荡不安，拉美、印尼、中国均如此。<br />
<br />
所以，必须在中国实行“改革开放加民主政体”的思想。目前，中国正在进行着一<br />
场革命与改制的赛跑，不改成多党竞选的民主政体，革命就会爆发。清朝末年，革<br />
命骤起，虽然有戊戌变法，但是已经来不及了，被革命一举摧毁。如今中国在政治<br />
上并没有进行任何导向多党竞选民主政治的改革，革命是在所难免的。<br />
<br />
十、权力制衡是民主的操作核心，多党竞选才能保证这样的制衡<br />
<br />
<br />
如果严格按照中国的法律规定行事，人民将比现在幸福很多。但是，看看中国法律<br />
的实际执行情况，恐怕我们无法乐观。其实，中国所有的社会问题，根本原因就在<br />
于中国缺乏权力制衡。一党执政，恰恰就是取消了权力制衡。<br />
<br />
“制衡”是民主的操作核心。权力必须要制衡，否则就不存在民主。任何一个不受<br />
制约的权力都会异化成人民的敌人。政党也是如此，一党执政，而没有在野党，这<br />
个执政党就不会受到制约的。两个以上的政党进行竞选才能互相监督、相互制衡。<br />
<br />
<br />
制衡才能遏制权力的滥用。在人民有权选择执政党的多党竞选的民主时代，政党的<br />
利益驱动就是为了获得执政权，执政党必须认真履行自己的竞选承诺，一心为民服<br />
务，争取连选连任，在野党千方百计地寻找执政党的错误和漏洞，在公开攻辩执政<br />
党的同时，修整自己的竞选纲领，寻求在下一轮竞选中胜出。制衡就这样产生了。<br />
任何一个政党执政时间久了，都会犯错，多党竞选、政党轮替才是人民的福祉。人<br />
民总是要选择对人民最有利的政党来执政。那些认为人民还没有素质搞民主的人，<br />
其实是把人民当白痴了。选择对自己有利的人来为自己服务，这是生活常识，并不<br />
需要多少文化素质。<br />
<br />
一党专政即使是天使也会走向邪恶。最典型的例子就是毛泽东执政时代，由于他不<br />
受到任何制衡，发动了一系列的整人运动，这些运动造成了中国史上最大的伤亡，<br />
从“整风”、“反右”、“大跃进”、“人民公社”到“文革”，在其掌权的几十<br />
年中没有受到任何挑战和质疑。结果给中国人民带来了无尽的灾难。由此可见，没<br />
有制衡的权力是多么的可怕，而且没有制衡的权力除了开始的三、四年，其后的时<br />
期一定是破坏性的，这样的例证不仅在中国，在世界的任何国家都会发生，如前苏<br />
联、北韩、古巴、前伊拉克等。<br />
<br />
制衡除了强化立法、司法对执政者的权力外，还必须要建立舆论的监督权，历史上<br />
很多残害人民的暴君都是被舆论打倒的，可见舆论的独立作用是多么重要。中国的<br />
宪法明文规定了言论自由，中国人民应该可以援引这一原则成立民办的报纸、电台<br />
、电视台。<br />
<br />
十一、兑现半个世纪前的民主承诺，建立多党竞选的民主中国<br />
<br />
共产主义是人类最高理想。其终极目标是物质极大丰富，人性极大发挥。我们首先<br />
要注意到的是这两个目标里，前者是经济，后者是民主。其次，我们还要注意到这<br />
两个目标不是一般指标性的目标，而是“两个极大”。从极限的理论我们可以知道<br />
，其实，说某目标是极大，其实也就是说，我们永远在路上。 <br />
例如，《大学》开篇第一句话，就是“大学之道，在明明德，在新民，在止于至善<br />
”。其中的“至善”，就是一个极限，于是我们知道，我们的一生就在一条不断向<br />
善的路上。<br />
<br />
如何才能使得我们的财富和我们的人性在通向“两个极大”的路上达到一个比较高<br />
的水平？这是我们任何一个对社会的发展和人民的幸福生活有使命感的人要充分思<br />
考的大课题。<br />
<br />
从古希腊的城邦民主到当代欧美的多党民主竞选，在人类的这些千百年来的艰苦卓<br />
绝的努力追求中，我们看到了不同执政观念的人民组党，然后进行多党民主竞选，<br />
是人类历史的主流。只有实施多党民主竞选的国家，人民才能在多党民主竞选中，<br />
获得最大的物质和人性收获。<br />
<br />
人类的生活，也就是经济、政治和文化三个层面。这“两个极大”在此三方面的运<br />
用为：<br />
<br />
经济上实行全民福利的经济政策，让人人都有生活保障，这样才能使得大家都发展<br />
自己的兴趣爱好，充分发挥自己的能力。<br />
<br />
在政治上实行多党竞选的民主体制，让各类对国家对民族自认为是最大效力的政治<br />
观点能得到人民的选择，这样才能使得人民从各类观点的竞选中获得最大的利益。<br />
<br />
<br />
在文化上实行百家争鸣百花齐放的民主政策，使得各种文化都能进行充分的辩论和<br />
展示，这样才能使得人民在各种文化中得到思想上的休息、训练和前进。<br />
<br />
全民福利的经济制度建立起来，民主制的政治制度建立起来，多样化的文化建立起<br />
来，这样整个社会的发展就不是在追求社会的财富和政治的特权，而是追求人自身<br />
的发展，而人的自由全面发展恰恰是共产主义最根本的含义。<br />
<br />
那种认为，经济上我们搞改革开放，发展经济，而政治、文化上我们搞一党专制、<br />
独裁统治的思想是极其反动的。我们的确需要发展生产力，但把我们的生存目的仅<br />
仅解释成是发展生产力，把人作为发展生产力的一种资源去加以配置，变成发展生<br />
产力的一种手段，就已经彻底的背离了人的本质，而把人变成了经济霸权和专制统<br />
治的奴隶。<br />
<br />
所以，除了在经济方面改革开放外，更重要的是政治上的多党民主竞选制度。当今<br />
中国的所有社会矛盾已经聚焦到当年毛泽东在延安窑洞里回答黄炎培的那个主题―<br />
―要不要民主的问题上了。毛泽东的回答是：“我们找到了一个好办法，那就是民<br />
主。”<br />
<br />
这个问题在上世纪40年代毛泽东和共产党人思考过，但是，他们最后却更多地担心<br />
他们的执政地位被其他民主党竞选掉，而在1957年发动了惨无人道的“反右运动”<br />
，把要求多党民主竞选的民主党人打成“右派”，无数民主人士被迫害致死。<br />
<br />
现在到了共产党人兑现共产党人对黄炎培的承诺的时候了，这个兑现虽然迟了半个<br />
世纪，但是这个兑现一定会受到人民的拥护的。这个兑现，就是建立全民福利条件<br />
下的多党竞选的民主中国。<br />
<br />
十二、国民党已经还权于国民了，共产党人也应该与人民共选<br />
<br />
一般人都知道孙文先生的三民主义是民族、民权和民生（三民主义在社会发展中实<br />
际运用的顺序是民族、民生和民权），也知道孙先生还有一个走向民主的“三政路<br />
线”是军政、训政和宪政，但是，很少有人把这两者结合起来思考。<br />
<br />
我认为，孙中山的这两个思维是根本不可分开的，其实质是认为解决民族的问题（<br />
驱除鞑虏恢复中华）必须依靠军政，解决人民的民生问题靠的是训政（一党专政）<br />
，而让百姓拥有民权，靠的是宪政（多党竞选的民主体制）。<br />
<br />
国民党是靠暴力革命驱除鞑虏的，后又用国民革命军平定了军阀混战，所以，这段<br />
时间应该属于军政。暴力革命，是一个阶级对另一个阶级的暴力，是被压迫者对压<br />
迫者的暴力，是没有办法而为之的革命行为。<br />
<br />
但是，暴力由于其手段与革命对像实施的手段完全一致，且暴力极易产生新的独裁<br />
专制而历来受到人民的高度警惕，所以孙先生认为，辛亥革命应该是中国历史上的<br />
最后一次暴力。<br />
<br />
孙先生认为，未来社会的前进，靠的不是暴力革命，而是宪政，即多党竞选。但是<br />
，军政之后，必然需要恢复因为战争而遭受破坏的社会建设，这个时期直接进入宪<br />
政，无论经济条件还是民智条件都是不够的，于是，在军政和宪政之间，根据中国<br />
的实际情况，孙先生安排了一个训政步骤。<br />
<br />
训政时期的特点是发展教育、开发民智，提高人民生活水平。这其实是民生的内容<br />
了。但是，历史和中国开了一个残酷的大玩笑，国民政府的训政受到来自日本的严<br />
重破坏。日本人的枪炮打碎了中国人的强国梦。<br />
<br />
等到1945年战争结束，中国的战争资源已经完全耗尽。此时，毛泽东领导的中国共<br />
产党和中国民主政团同盟等民主党派一起要求国民政府立即实施宪政。而国民政府<br />
认为战争刚刚结束，还不够宪政的时机。与此同时，前线因为受降问题发生国共冲<br />
突，最终爆发内战。<br />
<br />
1949年，共产党颠覆了国民政府，并把国民党（国民政府的执政党）驱逐到了台湾<br />
。<br />
<br />
国民党流亡台湾后，重新走了一遍孙文先生的三民主义和“三政路线”。首先，于<br />
1949午5月l9日颁布了戒严令，宣布台湾地区处于战时动员状况，实行军事管制。<br />
显然，这就是军政。其次，是封锁大陆消息，严禁一切违禁的言论、出版和罢工、<br />
游行等活动。1950年7月，国民党中常委临时会议通过了《中国国民党改造案》、<br />
将一些党政元老、军事将领、派系首领排挤出决策圈；全面更换旧有的党政系统；<br />
整肃党内旧有的派系。黄埔、中统、CC、政学系等皆被清除。同时，国民党统治集<br />
团整顿各级组织，进行党员登记，发展新党员，培植起一批拥护蒋介石、蒋经国父<br />
子的新实力派，建立了蒋氏父子对国民党的绝对控制权；经济上进行了财政整顿，<br />
改革币制，稳定物价，同时实行土地制度改革，恢复与发展农业生产。 这就是训<br />
政和民生。<br />
<br />
在 1986年 3月国民党召开的十二届三中全会上，蒋经国提出“政治革新”的主张<br />
，解除戒严、开放党禁报禁、调整“中央民意机构”、进行“总统”集体接班的部<br />
署等等。<br />
<br />
1988 年1月13日，蒋经国去世，随之李登辉执掌党政大权。李登辉在 1990年5月宣<br />
布开始“宪政改革”，对旧“法统”进行改造。从1990年至2000年，台湾当局进行<br />
了六次“修宪”，包括终止“动员戡乱时期”，废除 “临时条款”；“总统”由<br />
台湾地区人民直接选举产生。此举就是台湾的宪政。<br />
<br />
国民党最后还权于国民了，共产党也应该尽早与人民共选。目前的情况是，极少数<br />
的先富权贵控制着全社会90%以上的财富。同时，人民还无权与共产党人共同竞选<br />
执政党地位。<br />
<br />
事实证明，孙文先生的三民主义和三政路线是一条适合中国发展的有效道路。<br />
<br />
十三、前苏联解体是社会发展的必然，对中国有借鉴作用<br />
<br />
一段时期以来，社会上不断地有人在反思前苏联解体的教训。其实，前苏联的解体<br />
是人类历史的进步，是人类社会走向民主的一个环节而已。前苏联是苏联共产党在沙皇俄罗斯时代使用暴力革命建立的一个共产主义政权。列<br />
宁在十月革命前坚持认为共产主义可以在一国先建成，而马克思却一直认为共产主<br />
义必须在资本主义整体地达到最高级阶段以后，再经过暴力或非暴力，全世界一体<br />
进入共产主义。<br />
<br />
马克思之所以这样认为，是建立在大量的社会调查、精确计算和革命实践的基础上<br />
的。马克思认为共产主义的两个特征是物质极大丰富、人性极大发挥。而这两个特<br />
征必须经过资本主义的高级阶段才可以实现。<br />
<br />
但是列宁却错误地认为击败俄罗斯沙皇就可以进入共产主义了，而根本没有考虑物<br />
质是否极大丰富和人性是否极大发挥的问题。在这一点上，毛泽东和中国共产党也<br />
犯了完全相同的错误。毛泽东也根本没有考虑物质和人性的问题，而错误地认为只<br />
要把国民党政府推翻，就可以进入共产主义了。在这一点上，前苏共和中共犯了同<br />
样的错误。<br />
<br />
列宁最严重的错误，就在于他认为只要建立了共产党政权，就可以发展共产主义经<br />
济，而完全忽略了共产主义经济是人类历史全部经济的积累。毛泽东也犯了同样的<br />
错误。列宁在苏联搞了战时共产主义政策、毛泽东在中国搞了大跃进。结果是，苏<br />
联人民造反（大规模的农民暴动和士兵叛乱），而中国人民饿死。<br />
<br />
出现问题之后，列宁立即用新经济政策取代了战时共产主义，这就是列宁的伟大之<br />
处。他意识到自己错了，错在走的太快了，而忘掉了建设共产主义需要物质积累。<br />
所以，新经济政策的一项重要内容是以征收粮食税代替余粮收集制。列宁的一本书<br />
名说出了他对他的错误的认识，这本书叫《进一步、退两步》。<br />
<br />
可惜毛泽东并没有认为大跃进是错误的，相反，中国在饿死4000万人之后，没隔几<br />
年毛泽东又发动了文化大革命，结果更多的人死于非命。<br />
<br />
前苏联和中国之所以发生这么大这么多的灾难，其实归根结底是因为没有有效的制<br />
衡机制，即在野党的缺位。事实上，作为在野党，其神圣职责，就是对执政党的批<br />
评和言说。<br />
<br />
列宁死后，斯大林成为一党专制的主人。他立即废除了列宁的新经济政策，使苏联<br />
再次实行了“战时共产主义”。随后的几十年，苏联和中国分别都想“跑步进入共<br />
产主义”，结果人民的生活一落千丈。<br />
<br />
与此同时，腐败官员对社会财富的控制却越来越多。苏联各级干部的特权现象早在<br />
一党专政的开创时期就普遍存在的，二次大战后逐渐形成为特权阶层，享有各种既<br />
得利益。到勃列日涅夫时期，更形成一个个“官僚氏族集团”，这些集团内部儿女<br />
联姻，官官相护，贪污渎职，使执政党与民众之间隔阂越来越大，民心尽失。人民<br />
的积怨越来越来深，直到人民想要让这个伟大国家变成真正意义上的人民当家作主<br />
的民主国家。1991年12月25日,在克里姆林宫上空飘扬了七十余年之久的苏联国旗<br />
落下了。<br />
<br />
目前，俄罗斯国家在民主体制下度过了最困难的时期，俄罗斯人民的生活越来越富<br />
裕。 前苏联发生的事情，对21世纪的中国是有启迪作用的。<br />
<br />
<br />
十四、 民本思想和民主思想的区别<br />
<br />
中国自古以来就发展出一套很成熟的帝王学问，这就是民本思想。这是维持君王统<br />
治的法宝，也是封建专制主义有效对抗民主思想的法宝。<br />
<br />
近现代乃至当代，封建专制者为了反对民主体制，而有意误导人民，称“民本思想<br />
”就是民主思想，而中国人民错误听信了统治者的洗脑，这种状况使得中国一直处<br />
在专制主义统治下。<br />
<br />
民本思想，说白了就是封建君王把自己的极权统治当成是稳定压倒一切的大事，从<br />
而改善人民生活的一种封建帝王权术。帝王们认为，人民温饱了，于是就不造反。<br />
<br />
<br />
早期人类生活资源极其匮乏，人民随时处在饥谨边缘，封建帝王的这一用粮食换执<br />
政地位稳定的办法无疑是让人民吃上饭的好政策，受到人民的爱戴。《尚书五子歌<br />
》记载：“皇祖有训，民可近，不可下，民惟邦本，本固邦宁。”这段文字一方面<br />
强调了民众在国家政治生活中地位的重要，体现了对民众价值的深刻认识，一方面<br />
也设定了国家治理的终极目标――“邦宁”，对我国的封建政治产生了千古如斯的<br />
影响。<br />
<br />
战国思想家荀子亦有：“君者，舟也，庶人者，水也；水则载舟，水则覆舟”的论<br />
述。孟子也主张“民为贵，社稷次之，君为轻”（《尽心下》）。提倡“王道”政<br />
治，目的是劝告统治者重视人民，君与民同忧，同乐。<br />
<br />
夯实“民本”这个基础，政治统治就会稳固，社会就会安定。由于“民本”与政治<br />
休戚相关，千百年来“民惟邦本，本固邦宁”这一思想一直为政治家所笃信、所力<br />
行。<br />
<br />
隋末唐初，连年征战，人烟断绝，鸡犬不闻。唐太宗深知，如果不顾民众死活，横<br />
征暴敛，最终必如隋炀帝那样自食恶果。他说：“国以民为本，人以食为命，若禾<br />
黍不登，则兆庶(百姓)非国家所有。”（《贞观政要务农》）他还曾经在《贞观政<br />
要君道》中形象地阐述过这一观点：“为君之道，必须先存百姓，若损百姓以奉其<br />
身，犹割股以啖腹，腹饱而身毙。”这些话都明确地表明了唐太宗民本思想的本质<br />
：如不能“存百姓”，“则兆庶非国家所有”，就不能达到维护正常的统治秩序的<br />
目的。<br />
<br />
很显然，民本思想，其本质只是封建帝王的权术而已，其目的在于用粮食维持统治<br />
。维持统治古往今来有两种方式，一是军事镇压，一是经济诱惑。民本思想，属于<br />
后者。当然，所有的封建帝王都不会放弃军事镇压的方式来维持统治，哪里有反抗<br />
，哪里就有镇压。但是封建帝王也发现，中国人民的反抗最主要的原因还是粮食问<br />
题。在粮食问题成为一个生死攸关的问题的时候，先吃上饭比“持不同政见”要来<br />
得实在些。<br />
<br />
与其等到人民因为缺衣少食而造反的时候，帝国再派兵镇压，还不如先让人民吃上<br />
饭呢。毕竟吃上了饭还造反的人是少数的。于是，聪明的封建帝王，就开始落实这<br />
一思想，并美其名曰：“民本思想”。其实所有的帝王很清楚，这个“本”永远都<br />
是帝国统治的稳定。<br />
<br />
帝王的民本思想，的确在一定程度上解决了民生问题，但是其目的还是为了维持其<br />
专制统治。中国已经进入了21世纪了。人民再也不是只为了吃饱饭的“民生动物”<br />
。人之所谓被成为人，是因为其有思想和精神。国家有养活人民的责任。但是，不<br />
可以认为国家的责任只是喂饱人民。<br />
<br />
其实，当代中国的问题早就不是古代的那个是否能吃上饭的问题了，而是是否能公<br />
平分配社会财富的问题了。也就是说，现在人民已经把“民主”当成是维持民生的<br />
最重要手段了，而不是靠“明君”的名“民本”而实“帝王”的统治权术了。<br />
<br />
“民生”是任何一个执政党都必须解决的中国实际问题，解决人民的民生问题，绝<br />
不能成为我们不推进民主的理由。<br />
<br />
十五、取消工人罢工权力的执政党绝不是工人阶级的先锋队<br />
<br />
上几周，中国又发生多起罢工事件。罢工是劳资对抗的一种形式。这里的劳资双方<br />
是指劳动者和投资方。最近的工人反对资方的这些罢工主要针对的是资方的克扣工<br />
人工资、侵吞工资财产、没有劳动保护或其他侵犯工人权益的行为。例如，湖南江<br />
永国有有色矿因矿长每年从股份制企业公款中私吞260万人民币，工人不满开始上<br />
访，并在一个多月前罢工，10月3日，8名罢工工人被捕。10月10日，吉林长春市<br />
100多名出租车司机罢工，并到区政府请愿。反对把个体司机纳入私人公司统一管<br />
理，以免额外缴纳大笔管理费。10月14日，江苏省昆山市一家外资电子器材公司发<br />
生大规模抗议事件，员工因长期接触有害物质，有数百人的健康受影响，还有人因<br />
此患癌症死亡。员工要求厂方补偿医药费被拒绝后，全厂近2000员工集体罢工，并<br />
发起示威活动，多人被捕。<br />
<br />
<br />
关于工人是否可以用罢工的形式与资本家进行斗争的问题，我查阅了中国宪法。<br />
1954年宪法，1975年宪法和78年宪法都有关于罢工自由的规定，但是很遗憾的是，<br />
罢工自由被现行宪法（1982年）删除了。<br />
工人，就是做工的人。不到工厂工作就没有工资，就无法生存。而为什么工人不愿<br />
意到工厂做工呢？不到工厂工作有几种情况，一是“跳槽”，指工人找到了新的更<br />
好的工作而不到原单位工作，例如更高的工资、更好的工作条件和人际关系；二是<br />
“罢工”，指工人还继续要求在本工厂上班，但是却要求提高工资或改善工作环境<br />
、劳动保护等。<br />
<br />
中国人是最善良的人民，不到逼不得已是不会“罢工”的。更何况人民反对的还是<br />
做了坏事的资本家，我就想不通，人民和资本家的斗争，作为“工人阶级先锋队”<br />
的共产党为什么要制止呢？“工人阶级的先锋队”到底是要帮工人呢，还是要帮资<br />
本家呢？<br />
<br />
现在，有很多根本不想和人民共产的“共产党员”，自认为几十年前利用工人的“<br />
罢工”得了天下，就以为可以高枕无忧，可以横行霸道了。他们把持着国家和人民<br />
的命运，不帮工人与资本家做斗争，相反却与资本家穿一条裤子，或自己就充当资<br />
本家欺压人民，骂群众，压群众，而且人民想选择执政党他们也不让选，那么，每<br />
一个正直的中国人的愤怒完全是可以理解的。<br />
<br />
十六、必须构建以国学为体、西学为用的国民教育文化体制<br />
马克思的理论体系作为一种管理技术极大丰富了管理学的知识宝库。但是，马克思<br />
主义不可以取代中国文化。让马克思主义这一只有200年不到的德国思想来指导具<br />
有几千年的中国文化也是荒谬的。<br />
<br />
中国文化历史悠久、绵延不绝，是中国各族人民祖祖辈辈在共同的地域、语言、心<br />
理等基础上所形成的民族精神。<br />
<br />
中国人亲切把有关中国文化的学问称为“国学”。国学者何？一国所有之学也。有<br />
地而人生其上，因以成国焉，有其国者有其学。学也者，学其一国之学以为国用，<br />
而自治其一国也。尔今普及国学知识、推广国学思想乃势之必期而理之最明者也。<br />
国学的宗旨是“为天地立心，为生民立命，为往圣继绝学，为万世开太平。(《张<br />
子语录》)”<br />
<br />
国学是一个国家具有本国特色的文化知识体系，其目的当然是为人民服务的。至于<br />
所谓的国学糟粕，其实来源于被专制统治者利用之后发布的伪国学。这个伪国学，<br />
才是鲁迅在《狂人日记》中所说的字里行间流露着的两个字“吃人”。<br />
<br />
所以，鲁迅等一大批五四文化青年反对的根本不是“国学”，而是封建专制统治者<br />
的“伪国学”。1919年爆发的“五四”运动打倒“孔家店”和1966年开始的“文化<br />
大革命”“批孔”，其实都搞错了对象。在这点上，鲁迅和毛泽东是犯了同样错误<br />
的。<br />
<br />
现在，全世界各国都意识到国家文化作为一个国家标识，是一种增强民族凝聚力的<br />
最宝贵的文化财富。可惜的是，大半个世纪以来，由于中国宪法规定了使用马克思<br />
主义这一西方新思想来指导中国人的一切思维，以至于国学在中国成为一种“异在<br />
”，真正的文化意义上的中国人越来越少。<br />
<br />
目前，学国学的人却越来越少，而且错误地把国学当作封建糟粕加以批判和排斥，<br />
这不得不引起我们的高度警惕。<br />
<br />
作为一位长期研究国学的学者，我上书两位领袖，必须尽快研究构建以国学为体、<br />
西学为用的国民教育文化体制。<br />
<br />
中国人当以绵延数千年的中国传统文化为自己的国学，至于西方的新思想，的确有<br />
很多值得我们学习的地方，但是，不可以用西方新思想来限制、打击或消灭中国传<br />
统文化。<br />
<br />
研习国学，推进民主，正是当代知识份子应当敢于正视和承担的历史使命所在，过<br />
分强调和依靠一个生长在异域的德国思想已经无法光大中国这个已经不容乐观的民<br />
族了。在这一点上，我们可以借鉴台湾、香港以及新加坡等华文教学体制。<br />
十七、台湾问题只靠一国两制是不够的，恐怕还得走联邦宪政道路<br />
<br />
香港、澳门问题解决之后，中国最大的统一问题就是台湾了。<br />
<br />
其实，香港和澳门根本就不是问题，因为，香港和澳门是租界地，香港的期限是<br />
99年，而澳门的期限是永远。1997年，香港在到期后，英国把香港还回了中国，随<br />
后葡萄牙放弃了永远租用澳门的权利，也把澳门还给了中国。<br />
<br />
台湾问题，才真正算个问题。台湾问题是内战造成的。海峡两岸都是中华民族。<br />
<br />
台湾问题的所在是现在政体上差距太大。大陆是党政专制体制，而台湾是民主宪政<br />
体制。我认为，台湾问题上有三个问题要把握好：<br />
<br />
一是不到万不得已决不动武，中国人不打中国人；<br />
二是大陆的经济要奋起直追，大陆一直穷下去就永无希望。<br />
三是在政体上大概一国两制还不够，一种可能的方式是联邦制宪政之路。<br />
大陆经济上强大了，政治上又是全民福利条件下的多党竞选的民主宪政体制，台湾<br />
问题才有可能迎刃而解。<br />
<br />
十八、民主体制可以警惕并阻止权贵者用眼前利益劫持我们民族的未来<br />
<br />
没有监督的体制有一个很现实的问题，就是它关注眼前利益而无视民族的未来。对<br />
此人民看在眼里却由于没有民主体制而一筹莫展。<br />
<br />
例如，一些发达国家的人民为了国家和民族的未来，反对本国政府在本国生产有毒<br />
的化学工业产品，而充分运用民主体制赋予人民的权力进行了大规模的反对行动。<br />
其结果，就是这些国家在人民的压力下，撤消或销售掉这些有毒的化学装置。<br />
<br />
这些发达国家的有毒装置的巨大利润空间，被中国的权贵者看中，于是这些装置纷<br />
纷进入中国。全世界没有哪个国家会像中国一样，主动要求接纳并消化这些有毒的<br />
化学工业。例如，一时间中国的多个地区（厦门、南京、海南等）竞相上马了PX项<br />
目。国际组织规定这类项目要在距离城市100公里外开发，而中国根本不考虑其装<br />
置的危险性，把这些项目纷纷建立在交通方便的大城市里。<br />
<br />
中国的权贵者为了获得更大量的货币来满足他们日益增长的奢侈需求，他们不惜牺<br />
牲国人的健康和民族的未来。<br />
<br />
PX 全称“对二甲苯” （Dimethylbenzene），属危险化学品和高致癌物，而且遇<br />
氧只需27摄氏度就会爆炸。受到对二甲苯蒸气的慢性中毒能够引起神经错乱、头痛<br />
、嗜睡、作呕、血液组成起变化。人吸入71.4g/m3短时致死。当然，南京的人民群<br />
众一再被告知“国家有关部门会确保不发生爆炸和泄露事故”，但是，谁能保证没<br />
有意外呢？<br />
<br />
厦门的PX项目离市中心只有6公里，厦门人民由于没有民主体制无法对政府进行有<br />
效的控制而不得不用全体市民到市政府门口“集体散步”的方式表达人民的意愿，<br />
最后迫使该装置“缓建”。<br />
<br />
南京px项目的位置在离南京市中心大约20公里的栖霞山附近（南京炼油厂的厂区）<br />
，距离长江不到1公里。一旦发生问题，南京及下游的居民饮水就发生问题。离此<br />
装置不到1公里的地方，就是南京市民每年金秋最喜欢去的栖霞山，此装置离仙林<br />
大学城不到3公里，大学城里有10所全国重点大学。炼油厂的生活区里还有幼儿园<br />
、小学、中学、职工培训中心和范围很大的家属区，附近有栖霞镇、尧化镇等等，<br />
国家经济开发区――新港也就2公里左右。<br />
<br />
和厦门比起来，南京的px项目更可怕，这种剧毒化工装置一运行，南京意味着放了<br />
一个原子弹。<br />
<br />
其实，关于这类工程的选址，当地政府完全可以按照国际标准（人群居住地以外<br />
100公里以外），但是权贵者根本不考虑人民的生命和财产的安全，也不管后代的<br />
身体健康，更不管国家和民族的未来。他们只考虑便捷和成本低廉。<br />
<br />
他们的这种短视态度，其实是一种蔑视民族未来的可怕思想。他们只关注他们的当<br />
前利益，并用这一当前利益来劫持了本民族的未来。<br />
<br />
人民看在眼里，痛在心里，却没有民主体制进行反对。<br />
<br />
以上是我关于全民福利条件下的多党竞选的民主政体的全部思考。<br />
<br />
综上所述，中国的社会问题的一切症结就在于体制，我想，很多国人一定和我有一<br />
样的想法。但是我们也知道，在现有体制下中国的社会问题根本无法得到根治。<br />
<br />
于是，我提出这样一个政治模式。其目的在于，在确保人民的福利不受侵犯并不断<br />
提高的前提下，人民能够根据各自对幸福手段的思考而组织成各自的政党，然后，<br />
公开各党的施政纲领，在更大规模的范围内，让全体人民进行选择。所有各党必须<br />
是以促进国家发展、提升人民幸福为己任的。各党的宗旨只能是为人民服务，但是<br />
