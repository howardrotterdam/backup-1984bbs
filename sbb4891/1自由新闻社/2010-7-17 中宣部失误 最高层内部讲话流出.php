二、当前互联网发展趋势和面临的主要挑战<br />
当前，在新技术的推动下，世界互联网发展日新月异，出现一些新的特点：<br />
<br />
一是传统互联网加速向移动互联网延伸。移动通信技术的日益成熟，推动互联网从传统互联网向移动互联网的快速延伸，移动通信和互联网两大信息产业的融 合步伐进一步加快。目前，世界各国都在抓紧建设移动互联网，国际知名手机生产商和互联网企业都在加快技术研发，意图抢占移动互联网领域制高点。随着3G技 术日益成熟、手机智能化程度不断提高以及移动上网资费大幅下调，移动互联网将迅速普及。<br />
<br />
二是物联网将广泛应用。物联网的理念来源於每一个电子设备都成为一个终端，都可以通过互联网连接起来。电视、电脑、空调、热水器、电磁炉等设备出厂 的时候，里面都放置带有IP地址的芯片，通过无线嵌入到互联网或移动互联网，从而把所用的设备都连接起来，人们可以通过互联网对它们进行控制。物联网是互 联网发展的一个全新阶段，将对我们的工作、生活方式产生重大影响。<br />
<br />
三是“云计算”技术将使网民获取信息越来越快捷。【当前互联网业界流行一句话：“谁握住了那片云彩，谁就掌握了未来。”“那片云彩”指的就是“云计算”。】云就是计算机群，将很多位於不同地点的计算机集合起来，为企业或个人用户提供计算服务，每个群包括了几十万台、甚至上百万台计算机。网络用户通过计算机的简单功能上网租用“云空间”，由大型数据处理中心帮助保管个人的数据和记录，分享给限定的人群同步可以修改、联络。【目 前，电脑仍是我们网络生活的核心工具――处理文档、存储资料。如果电脑硬盘坏了，资料就会丢失。将来，“云”会替我们做存储和计算工作。我们不必在电话上 安装应用软件，可以直接连接互联网调用软件；不必在自己的电脑上存储数据，一切数据都存放在专业服务器上，不管是在何时何地，都可以随时取得数据。将来， 只需要一台电脑或一部手机，就可以通过“云”来实现一切。】目前，一些【IBM、Google、YAHOO等】国际互联网大公司都投入巨资加紧“云计算”技术的研发，并且在一些关键技术上取得了突破。<br />
<br />
随着互联网技术快速发展演变，近年来，我国互联网发展呈现出四个方面的新变化：一是在信息形态方面，信息传播形式以文字为主向音频、视频、图片等多 媒体形态转变。目前，我国网上音视频等多媒体信息占互联网流量50%以上，这极大丰富了网上信息的表现形式，同时也加大了信息内容安全监管的难度；二是在 应用领域方面，我国互联网正从信息传播和娱乐消费为主向商务服务领域延伸，互联网开始逐步深入到国民经济更深层次和更宽领域。这对於优化我国互联网消费结 构、促进经济发展模式转变具有积极意义，同时也对网络诚信建设提出了更高要求；三是在服务模式方面，互联网正从提供信息服务向提供平台服务延伸。以博客、 播客为代表的Web2.0服务模式使互联网的平台功能更加突出，网民不仅是信息的消费者，也是信息的提供者、创造者。这大大丰富了网上的信息内容，同时也 对网民的守法自律提出了更高要求；四是在传播手段方面，传统互联网也正在向移动互联网延伸，手机上网成为新潮流。基於移动互联网的新媒体形态不断出现，移 动互联网市场规模不断扩大，这大大拓展了网络信息的传播渠道，同时也对规&#65533;网络信息传播秩序提出了更高要求。<br />
<br />
互联网在我国的迅速发展，在提高我国信息化水平、促进我国经济社会发展进步的同时，也给我们提出了许多新情况新问题新挑战，我们面临的形势日益尖锐复杂。<br />
<br />
网络安全问题日益突出：网络淫秽色情等有害信息严重危害未成年人身心健康；网络欺诈、网络盗窃等违法犯罪活动严重危害公共安全，网络病毒、黑客攻击等严重威胁网络运行安全，网络失窃密等严重威胁国家安全和利益。【网 上问题产生的原因十分复杂。[从互联网技术本身特性看，互联网是全球开放信息体系，只要我国互联网与国际互联网联通，各种境外有害信息就有渠道和手段在境 内网上出现；只要我国互联网对公众开放，网民的各种言论就有渠道和手段在网上反映。从我国社会发展阶段看，当前我国处在社会转型期、快速发展期、矛盾凸显 期，现实社会中的矛盾和问题不可避免地反映到网上。从我国互联网管理实践看，我国互联网管理还处在探索、完善过程中，工作中还存在一些薄弱环节。这些问题 削弱了互联网管理的科学性、有效性，】制约了互联网健康持续发展，影响改革发展稳定大局，必须抓紧解决。<br />
<br />
三、进一步推动我国互联网科学健康有序发展<br />
【首先，要进一步明确我国互联网发展和管理的指导思 想。面对当前复杂的形势，我们必须以邓小平理论和“三个代表”重要思想为指导，深入贯彻落实科学发展观，坚持“积极利用、科学发展、依法管理、确保安全” 的方针，解放思想、实事求是、与时俱进、开拓创新，统筹国内国际两个大局，统筹当前与长远，坚持互联网发展与管理相统一，坚持行政手段与法律手段相统一， 坚持产业发展与网络安全相统一，坚持符合国际惯例与适应中国国情相统一，推动我国互联网科学健康有序发展，促进经济、政治、文化、社会建设全面进步，维护 社会和谐稳定和国家安全，走出一条具有中国特色的互联网发展和管理道路。】<br />
<br />
1、进一步提高互联网发展应用水平。【经过16年来的快速发展，我国互联网事业取得了很大成就，但总体水平还不高，互联网的普及率、应用水平、对国民经济的贡献及企业国际竞争力，相比美国等互联网发达国家仍有相当大的差距。】这要求我们必须在新的起点上进一步加快发展，推动互联网又好又快的科学发展，以发展解决发展中的问题。【当前最为紧迫的是要进一步明确互联网发展的整体战略思路，】 加快调整互联网应用结构，大力发展有利於促进我国经济建设、提升我国网上公共服务水平的互联网应用，促进互联网在经济、政治、社会等各领域协调发展，大力 促进电子商务、电子政务、电子教育的发展，加快各类为民、便民、利民信息内容建设，引导人们把互联网广泛用於工作、学习和商务活动，形成结构合理、比例适 当、发展均衡、健康规&#65533;的网络应用格局，同时，要加强互联网核心技术研发，开展重大项目攻关，提高自主创新能力，掌握领先核心技术，提高我国互联网整体发 展水平。<br />
<br />
2、加强网络文化产品创作生产。适应三网融合发展需要，大力发展中国特色网络文化，为人民群众提供更多更好的网络文化产品和服务，使积极健康的网络 文化产品占据网上主导地位。一是抓好网络文化工程。制定促进网络文化发展规划，加快信息资源开发利用，推进中国数字图书馆、国家知识资源数据库、数字化多 媒体研发等网络文化工程项目建设，构建公共文化信息服务体系；二是抓好网络文化创新。把博大精深的中华文化作为网络文化的重要源泉，推动优秀传统文化瑰宝 和当代原创文化精品的网络化、数字化，努力打造一批具有中国气派、体现时代精神、品位高雅的网络文化品牌；三是抓好网络文化产业。加快传统文化产业与网络 文化产业的融合，鼓励国有战略投资者投资网络文化企业，培育扶持一批国有或国有控股的、实力雄厚的网络文化企业和企业集群，不断提高网络文化创作生产的规 模化、专业化水平。<br />
<br />
【3、加强网上舆论引导。建立和完善网上舆情研判机 制，掌握网上舆情动态，及时发现苗头性、倾向性问题，增强网上突发热点的预警能力。进一步完善网上舆论工作机制，形成网宣部门总体协调、实际工作部门主动 应对、重点新闻网站发挥主渠道作用，宣传、引导、管理协调配合的网上舆论引导工作格局。各地各部门出台重大政策和工作举措、处置重大突发事件，要坚持及时 准确、公开透明的原则，制定网上舆论引导预案，及时解答网民疑惑，避免关键时候失语，延误舆论引导时机。按照中央文化体制改革总体要求，加快重点新闻网站 体制改革步伐，抓紧实施中央重点新闻网站搜索引擎、国家网络电视台等重大项目，进一步增强重点新闻网站的综合竞争力。加强重点新闻网站对外传播能力建设， 有针对性开展国际舆论斗争，营造於我有利、客观友善的国际舆论环境。】<br />
<br />
【4、加强互联网法制建设。从我国国情出发，借&#65533;国 际有益经验，完善我国互联网法律制度。加强互联网立法的统筹规划与总体协调，加强互联网法律法规建设的前瞻性研究，定期梳理有关互联网管理的法律、行政法 规和部门规章，根据互联网发展和管理工作实际需要，及时制定新法、修订现有法律法规、废止与实践不相适应的规定，加大司法解释力度，增强法规的可操作性。 建议尽快制定颁布《&lt;全国人大常委会关於维护互联网安全的决定&gt;实施细则》，进一步明确有害信息的判定标准、执法主体和处理程序。抓紧修订 《中华人民共和国电信条例》、《互联网信息服务管理办法》、《互联网电子公告服务管理规定》、《非经营性互联网信息服务备案管理办法》、《互联网络域名管 理办法》、《互联网IP地址管理办法》等法规，通过立法进一步明确域名注册服务者、互联网接入服务提供者、信息服务提供者和信息发布者等主体的权利义务和 法律责任。尽快出台《手机媒体服务管理办法》，为手机媒体管理和推行手机实名制提供法律依据。根据信息化网络化发展情况新问题，加快对刑法、民法、保密法 等法律法规的修订工作。抓紧研究互联网管理基础法律，尽早启动《互联网管理法》的立法工作。研究制定《信息安全法》，明确网络与信息安全法律的基本原则， 明确各类主体的基本权利、义务和责任。】<br />
<br />
3【5】、进一步加强互联网基础管理。一是进一步加强网站域名、IP地址、登记备案和接入服务管理。严格落实域名注册和IP地址备案管理相关规定。完善网站登记备案制度。加大对接入服务企业日常监管力度，严禁接入服务企业为未经备案或许可的互联网站和手机网站提供接入服务。【二是积极稳妥推行网络实名制。尽快实现网站实名制，全面实行手机实名制，逐步在网络互动环节推行实名制。三是建立健全网络信息安全制度。研究制定互联网新技术新业务的应用标准和安全规&#65533;，建立互联网新技术新业务和外资企业进入我国互联网市场信息安全评估机制。】建立健全电信运营企业、网络运营企业、接入服务企业、域名注册服务机构等信息安全责任制，完善信息安全保障措施。<br />
<br />
4【6】、加大打击网上有害信息和网络违法犯罪活动力度。【完善互联网有害信息监控工作体系，加强对境外有害信息的封堵，有效防止境外有害信息通过互联网在境内传播扩散，抵御境外敌对势力的网上渗透；】严格规&#65533;互联网信息传播秩序，提高网上有害信息处置效率，严防网上有害信息大&#65533;围传播扩散，维护社会和谐稳定；继续开展互联网专项清理整治工作，依法严厉打击网络色情、赌博、诈骗等网络违法犯罪活动，严惩违法犯罪分子，规&#65533;互联网安全秩序，净化网络环境。<br />
<br />
【7、理顺互联网管理体制。参照国外互联网管理的有 益经验，结合我国互联网发展和管理的实际，按照“统分结合、相对集中、职责明确、权责一致”的原则，尽快理顺体制，建立既有利於解决当前互联网管理的现实 问题，又有利於构建长远的互联网管理架构；既有利於强化互联网管理主要部门职责，又有利於调动各个部门的工作积极性；既符合国际通行做法，又有中国特色的 强有力的互联网管理体制，提高互联网管理效能。推动互联网科学健康有序发展，需要法律法规的支撑。在全国人大的有力指导和大力支持下，我国互联网法制建设 取得重要进展，推动了互联网在规&#65533;中发展、在发展中规&#65533;。目前，我国互联网发展正处於关键阶段，对依法公开管理提出了新的更高要求，希望全国人大继续给我 们以指导、帮助和支持，使我国依法管网工作迈出新的步伐。<br />
<br />
谢谢大家。<br />
（主讲人为中宣部副部长，中央外宣办、国务院新闻办主任）】<br />
