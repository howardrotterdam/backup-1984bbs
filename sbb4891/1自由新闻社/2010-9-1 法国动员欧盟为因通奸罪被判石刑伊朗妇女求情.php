　　法国外交部一位发言人向媒体通报说：“萨基内・穆罕默迪・阿什蒂亚尼的状况令我们十分担忧。法国将和欧盟伙伴国共同研究她的案件，以找出一切可能的办法，避免石刑的最终实施。”<br />
