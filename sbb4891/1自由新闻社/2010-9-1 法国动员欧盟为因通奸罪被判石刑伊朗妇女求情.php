<br />
　　据报道，伊朗寡妇萨基内・穆罕默迪・阿什蒂亚尼因“通奸罪”于2006年5月被法院判以鞭刑，随后又被另一家法院判处石刑――即将犯人埋入土中，只露出头，然后用石块砸死。2007年5月27日，伊朗最高法院核准了对萨基内的死刑判决。<br />
<br />
　　今年7月底，巴西总统卢拉为萨基内求情，希望伊朗对其改判死刑为缓刑，并表示巴西愿为她提供避难所。</div>

							
							
							
							
