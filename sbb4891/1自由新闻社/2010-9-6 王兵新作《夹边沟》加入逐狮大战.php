													<h2>王兵新作《夹边沟》加入逐狮大战</h2>
						
						
																											<div id="postmessage_816362" class="t_msgfont">中国片再次惊喜威尼斯 王兵新作加入逐狮大战<br />
<a href="http://www.sina.com.cn" target="_blank">http://www.sina.com.cn</a>&nbsp;&nbsp;2010年09月06日10:56&nbsp;&nbsp;新浪娱乐<br />
中国片再次惊喜威尼斯王兵新作加入逐狮大战<br />
<img src="http://i1.sinaimg.cn/ent/r/m/2010-09-06/U4611P28T3D3078135F326DT20100906181504.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
导演王兵(资料图)<br />
<br />
　　新浪娱乐讯 2006年贾樟柯《 三峡好人》成为威尼斯惊喜电影并一举夺得金狮，四年后，2010年的第67届威尼斯电影节上，中国王兵导演的新作，(意大利片名《il fossato 》)在最后一刻再次以惊喜片身份亮相，为华语片夺狮增加力量。<br />
<br />
　　将在当地时间6日早上8点半钟以惊喜片身份揭开神秘面纱的影片，提前被意大利本土媒体爆光身份。本部参赛新片是根据小说《告别加边沟》改编，由中国、中国香港、法国和比利时四地联合制片。影片的葡萄牙制片人弗朗西斯科-维拉-罗伯斯介绍在今年都灵和王兵认识，并诞生了这部影片的创作设想。<br />
<br />
　　出生于1967年的王兵，曾以讲述沈阳铁西区工人状态的长达9个小时的纪录片，赢得国际影评界的关注并获得声誉，此后，他相继拍摄了《和风鸣》，《煤钱》等纪录片，均在戛纳影展、巴黎真实电影节等权威平台上亮相。 据影片制片称，新片在参赛威尼斯电影节后，还将马不停蹄赶往多伦多电影节。<br />
<br />
　　威尼斯电影节在马可－穆勒主持下创立的惊喜电影，往往在最后上映一刻采揭晓真面目，并且战绩不错。第一届韩国导演金基德的《空房间》获得评委会大奖，贾樟柯《三峡好人》贵为金狮。此外，日本北野武、菲律宾曼多萨的《罗拉》等作品也以此种形式亮相水城。华语电影和这一特殊身份颇有缘分，不仅有2006年贾樟柯《三峡好人》打头阵，2008年杜琪峰《神探》，时隔两年，华语片再次成就惊喜。 新浪前方报道组 (刘敏)<br />
<br />
<br />
《夹边沟》评论：冷静直接地回顾历史<br />
　王兵的首部故事片《夹边沟》，是一部冷峻、直接的电影，几乎没有任何回避地回顾了那段历史。王兵将纪录片创作的美学引到故事片中，用独特、自信的视听语言展示了在1960年前后，人间地狱一般的夹边沟劳改农场发生的故事。<br />
<br />
　　《夹边沟》根据杨显惠小说《告别夹边沟》改编，原著虽然大多有原型和真人真事做基础，但由于事件的隐蔽性，也不可能做到完全地揭示真相。王兵在创作过程中积累了大量的素材和资料，他之前拍摄的长达三个小时的纪录片《和凤鸣》，就是以夹边沟事件的幸存者和凤鸣口述作为载体，记录了个体数十年的人生经历。<br />
<br />
　　王兵自幼丧父，看杨显惠《夹边沟纪事》后，才知道父死于夹边沟，尸骨无存，不知道这是不是他创作该片的动机。影片在2008年10月开始拍摄，2009年1月完成，历时75天，积累了130小时的素材。<br />
<br />
　　尽管电影中有极其直接的描写，比如劳改犯由于过度饥饿吃下其他人吐出的食物，死人被随意暴露在戈壁沙漠，有人从侧面说出人吃人的事实。但相对于小说所描写的更严苛的情况而言，电影的表现还是较为收敛。但即便如此，还是有为数不少的观众忍受不了写实的描写，选择了提前退场。<br />
<br />
　　电影冷冽之处在于，几乎所有的镜头都没有感情，只是选择合适的方式冷静地记述，常年的劳动和精神打击，以及食物的短缺让劳改犯人极度麻木，在这里死亡每天都会出现，其他人要做的，只是用被子把他们裹起来，然后扔掉或者掩埋，甚至把他们的衣服扒光，换成吃的。劳改犯居住的壕沟外面，是一望无际的戈壁，没有尽头，没有希望。人们在这里死去，在这里煎熬，找不到任何出路。<br />
<br />
　　王兵拍摄纪录片的自信和制作方式，依旧延续到了《夹边沟》中，电影中几乎所有的演员都有着让人印象深刻的“出现”，而非表演。其中曾亲历夹边沟事件的李祥年还在电影中出演了一个角色，他帮助许多人更深刻地了解了这个故事。美中不足的的是其中一位死者的妻子，她以闯入者的身份来到劳改农场，得悉丈夫死去的事实后号啕大哭，并且执意要寻找到丈夫的尸体。她的表演过于外放，与电影中人物的状态明显不符，即便她是一位“局外人”，但也不应该表演的如此极端。<br />
<br />
　　拍摄纪录片存留下的长镜头美学，在《夹边沟》中也得到了切实的体现。王兵关注声音的空间层次，从头至尾没有音乐，关注画面景深的处理，关注空间、景别、用光的变化和节奏，有条不紊地铺陈故事。虽然采用手持摄影，但画面的运动保存了强烈节奏感和控制力，几乎每一个镜头都有经过深思熟虑的调度。但由于某些原因，一些镜头的剪辑和创作还是有不是那么严谨的处理，但瑕不掩瑜。<br />
<br />
　　《夹边沟》最大的意义在于，用电影的方式直面了那段历史。也许这不是事实的全部，但足以让人难以忘记。<br />
<br />
　　新浪威尼斯报道组/文</div>

							
							
							
							
