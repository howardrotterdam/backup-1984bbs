													<h2>北京晚报：一些不负责任的论坛说出租车里有窃听器</h2>
						
						
																											<div id="postmessage_261185" class="t_msgfont">近日有境外媒体声称，十一前夕，北京的出租车上都安装了微型监听器，用于“窃听”乘客在车里的谈话；国内一些不负责任的网站论坛和博客也转载了这则消息。昨天在接受境外媒体记者采访时，北京市交通管理局副局长隋亚刚澄清说，这种传闻不属实。<br />
<br />
　　“我从来没有听说过你所说的情况。”当有境外记者抛出有关“窃听器”的传闻时，隋亚刚严肃地进行了回答。<br />
<br />
　　根据境外媒体记者描述，所谓“窃听器”是指安装在部分出租车挡风玻璃前类似麦克风的装置，对此，隋亚刚解释说，这个装置仅仅是一个普通的GPS设备，是用于采集路况交通信息而安装的。<br />
<br />
　　隋亚刚解释说，交管部门在进行交通流量监测时，除了要利用全市快速路、主干路网上预先埋设的监测装置外，还通过在出租车上安装的设备，实时了解路况信息。<br />
<br />
　　另外，在出租车上安装GPS设备，还能够对车辆所处位置进行定位，对于出租车司机和乘客的人身安全，也是一种有效的保护措施。<br />
<br />
　　听到这则捕风捉影的传闻，一位常年居住在北京的记者也表示不解：“我几年前就在出租车上见到过这个装置了，怎么能说是为了今年国庆而进行‘窃听’呢？”<br />
<br />
　　通过网上检索也可以发现，其实在去年奥运会期间，就有境外媒体耸人听闻地报道过此事，甚至连描述细节和由此引发的论调，都和近期的报道几乎雷同。<br />
<br />
　　对此，有居住在美国的华侨撰文说，很多国家的出租车上，都配备了带有双向麦克风的GPS设备，特别是在美国，这种设备早已司空见惯，却从来没有任何媒体抱怨过此事，更不会将其联想为“窃听”乘客谈话。一些境外媒体对于中国似乎总是抱有特殊的想象力，而这种以讹传讹的报道显然也经不起推敲。(记者周健森)</div>

							
							
							
							
