<br />
　网友发布的图片显示，一名年轻的女子身着一件印有中华人民共和国国旗图案的短裙在大街上行走，显得格外乍眼。根据网友的帖子和周围环境判断，这名女子出现的地方应该是在西安市的窗口景区大雁塔附近。<br />
<br />
　　有好奇网友判断：这条裙子明显是用国旗直接做成的，展开了就是一面国旗，而发帖的网友显得更加激愤，称其恨不得“上去一把把国旗扯掉！”<br />
<br />
　　对于这名女子的异常行为，网友“娃他大舅”叹息道：唉！丑人多作怪！更有网友称其无知，并打趣道“无知者无畏”！<br />
<br />
　　而网友“半开的花朵”则对她表示理解：“说不定她是太爱国了，太想证明自己是中国人了，所以才有此举动！”<br />
<br />
　　记者通过了解发现，身着国旗装并非是什么稀奇事，在“水立方”中举行的第六届中国泳装设计大赛总决赛上，就有美女模特身披国旗设计泳装在水立方婀娜漫步，当时也引起了网友的激烈讨论。<br />
<br />
　　有支持的声音称：身穿国旗装无伤大雅，细看《国旗法》的规定，单从第十八条规定去理解，只要不是用作商标和广告，只要不是恶搞，在时装上印上国旗图案以示爱国，应该不算违法，就象球迷在脸上用油彩画上国旗图案为中国队加油助威一个道理。<br />
<br />
　　反对的一方则表示此举对国旗不敬，水立方的比基尼时装模特身披五星红旗走秀，将国旗图案印在比基尼上恐怕应算是对国旗的玷污。我认为主办方在活动前没有考虑到中国老百姓的接受程度，该受谴责。<br />
<br />
