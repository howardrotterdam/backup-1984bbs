																											<div id="postmessage_410630" class="t_msgfont">南京徐老太”死了！<br />
墨心人<br />
　　<br />
　　　今天上午有人在某论坛发了一个简单的帖子，标题和正文都只有一句话：“南京徐老太死了”。<br />
　　　看到这个消息，我的心突然洋溢着喜悦，就像是听到一个恶贯满盈的罪犯被毙了一样，就像是听到薄熙来王立军在重庆铁腕打黑一样，真想痛痛快快地去喝一回酒，再到卡拉OK去五音不全地吼上一通。我甚至没有兴趣去证实这消息是不是真的，就已宁愿相信是真的。这个南京徐老太在我心里压抑了几年，如今终于死了，这是一件多么好的事情呀。范伟在《卖担架》中的呼喊，在我耳畔一遍遍地重复着：唉呀，唉呀呀呀……苍天呀大地呀，这是哪位天使大姐替我出了这口气呀！<br />
　　　喜悦完后，心里随之是空落落的感觉，她毕竟和我素不相识，生老病死又与我何干？面对一个生命的永远离去，我报以欣喜的心态，是否有失人类的厚道？也许对于她的儿孙们来说，她还很可能是一个慈祥的老人，她的离去，势必会引发亲人们的悲伤，在这新年即将到来的时刻，我有啥好高兴的？即便是素不相识的人死了，我也该寄上一份素不相识的哀思，才符合作为一个人的本份。<br />
　　　然而无可否认我确实是很不厚道地高兴着，甚至来不及分辨这种高兴究竟是高尚还是卑鄙，就禁不住高兴了起来。<br />
　　　可能很多人不知道这个南京徐老太，因为一个素不相识的老太太的姓氏，实在没有记住的必要。但是她做过一件事，当初知道的人估计永远都不会忘记。回放一下过程：2006年11月20日，徐老太在街上自行摔倒，路人大多不顾而去。南京青年彭宇见状，主动上前搀扶，并把她送到医院检查，还垫了两百块钱医药费。这么一件救助老人的好人好事本来这样结束就很好。但是被救助的徐老太因为那一次的摔倒引发的骨折，花掉了十来万块钱医治，总得找个人分担一下。因此，她在几个月后把彭宇告上了法庭，说是他把自己撞到了，要负担医药费。<br />
　　　彭宇就这样被讹上了。<br />
　　　此案唯一的目击证人陈老先生在案发时也参与了部分救助，他看到的情况是：老太太手里拎着保温瓶，向第三辆公交车跑去。她跑到第二辆车的车尾时，不知为什么就跌倒了。这时，他看到从第二辆车后门下车的彭宇走了几步，上前帮忙，然后自己也上前帮忙，并打电话叫老人的儿女过来，整个过程大约半个小时。<br />
　　　2007年9月，南京市鼓楼法院判决的结果是：彭宇负担徐老太40%费用，合计45876.36元。走出法庭，彭宇说：“以后再也不会这么冲动了！”陈老先生比彭宇还激动，对着摄像机大声说：“朋友们，以后还有谁敢做好事？”<br />
　　　此恩将仇报的事件轰动南京市，继而轰动全国。<br />
　　　当时就有网友炮制了一个“救助宝典”，大致精神是：看见徐老太太倒下后，先去找个相机或者摄像机把当时的情形拍下来，然后再找当时在场的人写下目击证明，然后再起草一份《救助协议书》，说清楚是徐老太自己倒下的，让她签字画押。做完这一系列工作之后，才能考虑开始救助行动，如果这时候徐老太还活着的话。<br />
　　　此事的直接恶果还是发生在南京市。2009年2月22日上午，一位75岁的老人在南京市下关区三汊河公交站台下车时一头栽倒在地，路人很多，但没人敢上前救助，老人为求得帮助，只好大声喊道：“是我自己跌的，和你们无关，你们不用担心！”<br />
　　　老人此话幽默得让我眼睛人发酸。<br />
　　　其实人心都是肉长的，能帮人的时候伸手帮一下，也是功德一件，何乐而不为？<br />
　　　问题在于帮助之后如果被反咬一口，怎么办？有网友说得好：“交不起扶务费！”更有人列举了雷锋当年做的一系列好事之后，得出了一个结论：要在放在今天，雷锋会被罚得倾家荡产。<br />
　　　此事让爱心止步，让本来就日下的世风来一次跳水，给本来就沦丧的道德再来一次蹦极跳，给不想做好事的人以很好的借口：我怕她讹上我！给想做好事的人以很好的阻吓：我怕她讹上我！有人说，对这类人，无论是徐老太太还是徐大姑娘，就该见一次揍一次，让他们的骨头永远处于断裂状态。<br />
　　　这话过于激愤，不过徐老太……她的心，当诛！<br />
　　　现在，南京徐老太死了――哪怕网上这个消息是假的，她还活着，我也希望她尽快死去――代表着人类中的这部分卑鄙无耻的灵魂集体性地死去，死个干干净净――我这念头很恶毒，对他们，我除了恶毒，没有第二种想法。<br />
