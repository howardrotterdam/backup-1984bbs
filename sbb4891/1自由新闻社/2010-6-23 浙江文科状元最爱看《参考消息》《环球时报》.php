文科最高分：钟隽仪 <br />
<br />
速写：爱看《参考消息》，平时很低调 <br />
<br />
听到自己高考分数是“725”时，钟隽仪正在从上海赶回海盐的路上，时间指向晚8：20，早上她在世博园玩得正尽兴。记者打给她的时候，她还不知道各科的成绩，所以她也有兴趣再次向记者证实和确定：语文129分、数学148分、外语139分、综合253分、模块56分。 <br />
<br />
钟隽仪是海盐元济高级中学的学生，是个女生。班主任张强教地理，也能归于文科类教师，对钟隽仪很熟悉。 <br />
<br />
他说：“我问了一些孩子，好像数学成绩不太理想，所以也担心钟隽仪的。结果，她很稳，考了高分。”钟隽仪认为高考数学题的难度和平时训练的差不多，解题方法多种多样。考数学当天，她速度还算理想，最后足足检查了两遍，所以148分对她来说并不意外。 <br />
<br />
老师眼里，钟隽仪是个低调的孩子。身为班长的钟隽仪有机会竞选学生会主席，但她最终选择“竞聘”学习部部长的职位。钟隽仪一再表示，这是尊重，她尊重原来的学生会主席，认为这个同学已经付出一年的辛劳了。 <br />
<br />
钟隽仪和同龄孩子一样，哈韩。但不同的是，她懂得兼顾学习。她的知识面宽广，并不拘泥于课本知识。<strong>平时最爱看《参考消息》、《环球时报》</strong>,最喜欢的杂志是《国家地理》。<br />
<br />
钟爸爸是当地一所小学的数学老师，妈妈是当地工商所的工作人员。这户家庭知道如何科学地教育孩子。父亲说：“<strong>孩子两岁开始看报纸，看过三遍的马上能记下相关内容。</strong>”所以，她的阅读习惯非常了得。 <br />
<br />
“看的书多了，知道的东西自然也多。”钟爸爸一直很赏识自己的孩子，但这种爱很理性。 <br />
<br />
元济高中的副校长吴水林告诉记者，元济是1999年创办的公立中学，到了2005年3月份，她成为省一级高中。今年出现钟隽仪这个文科女状元不意外。因为学校文科优势非常明显，2001年全省前100名文科学生，元济占了五位。2004年前100名文科生，元济占了四位，到了2009年，已经占到六位。 <br />
<br />
吴校长同样提到了新课改的研究，这是学校高速发展的关键因素。吴老师认为他们的研究型教学管理模式是成功的。“2003年至2005年，我们还大量扩班，很多年轻老师成为学校的新鲜血液。” <br />
