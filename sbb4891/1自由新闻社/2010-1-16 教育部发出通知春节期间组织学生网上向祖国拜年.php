													<h2>教育部发出通知春节期间组织学生网上向祖国拜年</h2>
						
						
																											<div id="postmessage_435208" class="t_msgfont"><a href="http://www.gov.cn/gzdt/2010-01/16/content_1512551.htm" target="_blank">http://www.gov.cn/gzdt/2010-01/16/content_1512551.htm</a><br />
<br />
教育部1月15日发出通知，要求全国各级各类学校，于春节期间组织学生参与“给祖国母亲拜大年”活动。<br />
<br />
&nbsp; &nbsp; 为认真贯彻落实党的十七大提出的弘扬中华文化、建设中华民族共有精神家园的战略部署，把社会主义核心价值体系融入传统节日活动之中，在春节期间营造欢乐喜庆、文明祥和、温馨和谐的节日氛围，掀起全民爱国热潮，中央文明办、教育部、解放军总政治部、全国总工会、共青团中央、全国妇联等六部门决定，于春节期间在网上开展“给祖国母亲拜大年”活动。<br />
<br />
&nbsp; &nbsp; 据了解，此次活动的时间为1月10日至2月19日。活动将通过中国文明网、央视网等媒体，以图片和文字形式向学生征集春节吉祥物、春联、拜年短信、各地民俗，特别是如何“给祖国母亲拜大年”的好创意。活动还将以中国文明网、央视网为主体，推出网上活动平台，鼓励各级各类学校学生，通过多种形式向祖国母亲拜年。拜年网页的内容设计为：一拜壮美河山，二拜炎黄始祖，三拜历代英杰，四拜革命先烈，五拜英雄模范，六拜亿兆黎民。对挑选出来的精华内容，将进行网上展示和手机传递。<br />
<br />
&nbsp; &nbsp; 教育部通知要求，各地教育行政部门和各级各类学校要高度重视此次活动，因地制宜地做好宣传组织。要将此项活动纳入加强和改进未成年人思想道德建设和大学生思想政治教育工作，认真部署安排，体现出教育系统的特色。<br />
<br />
&nbsp; &nbsp; 据悉，有关部门为此次活动设立了专门的网站，网址为：<a href="http://hxd.wenming.cn/dbn/index.htm" target="_blank">http://hxd.wenming.cn/dbn/index.htm</a>。此外，登录中国教育新闻网也可查询此次活动。（记者 焦新）</div>

							
							
							
							
