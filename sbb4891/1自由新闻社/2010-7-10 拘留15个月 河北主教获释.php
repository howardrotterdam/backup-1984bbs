拘留15个月　河北主教获释<br />
<br />
<img src="https://yopic.us/images/10ce7p.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
梵蒂冈宣布，去年3月被中国当局逮捕的河北正定教区正权主教贾治国（图），在拘留15个多月后已在本周三（7日）获释。75岁的贾，因为效忠罗马天主教教宗，不肯加入大陆官方的中国天主教爱国会而多次遭到逮捕。去年3月30日，他在主持一项宗教会议后，遭当局拘捕。<br />
<br />
贾治国拒加入大陆官方教会<br />
<br />
据教会消息人士对天亚社说，贾主教回到省会石家庄附近武邱村的基督君王主教座堂后，于晚上为教友主持了弥撒。贾的家人曾多次要求政府释放他，或「至少让他们见见面」。而罗马教廷曾表示，贾的事件是梵蒂冈与北京间对话的障碍。教廷并对其他传教士因为参与宗教活动而遭到关押或骚扰表示关切。<br />
<br />
出生于中国河北省晋州武邱村的贾治国，热心天主教。1963年到1978年服刑15年。2004年以来，他陆续被当局拘捕13次。2009年3月30日，他在武邱村基督君王主教座堂被公安人员带走，当天正是教廷中国教会问题研究委员会于罗马召开年度大会的第一天。<br />
<br />
据人权团体表示，在中国，效忠梵蒂冈的天主教神职人员经常受到迫害，主教遭到关押是家常便饭。除贾治国外，另有两位河北省地下主教──保定教区苏哲民（志民）主教及易县教区师恩祥主教，分别自1997及2001年被拘留后，至今下落不明。<br />
<br />
