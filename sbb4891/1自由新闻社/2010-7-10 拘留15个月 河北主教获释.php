??立刻我想到了太阳。事情就是这样的。一夜之间我成了个拜日教徒。当然，不是在一夜之间，因为夜里看不见太阳。但是，第二天早上的第一件事儿，就是开始崇拜太阳。几个因素。首先，我可以看到太阳，OK？不像我提过的其他神，我可以真正看到太阳。这一点很重要。如果我能看到某样东西，我不知道，也许更值得信赖，你知道吗？所以，我每天都可以看到太阳，而它给了我所需要的一切，热，光，食品，公园里的花朵，湖上的涟漪，以及存在偶然性的皮肤癌。但是嘿，至少没有十字架钉刑，而且我们也因为谁不同意我们而烧死他/她。<br />
<br />
??太阳崇拜就是这么简单。没有神秘，没有奇迹，没有排场，没人要钱，也没有什么颂歌要学，而且我们也没有特殊的场所好让我们每周聚集一次比较一下着装。而太阳的最好之处，是它从来不对我说我不配。从来不对我说我是一个需要被救赎的坏人，从来不说一个带恶意的词儿，对人和善。所以，我崇拜太阳。但是，我并不向太阳祈祷。知道为什么吗？我不想破坏我们之间的友谊，那可不礼貌。<br />
<br />
??我常觉得人们对待上帝太粗暴了，啊是？每天提出亿万计的要求。向上帝要求恳求乞求青睐。做这个，提那个， 我要辆新车，我想换个好工作... 而大多数的祈祷都发生在礼拜天，他的休假日。这个不礼貌， 可不该这样对待朋友。<br />
<br />
??但人们依旧祈祷，而且他们为了许许多多的事儿祈祷。你知道，你姐姐需要裆部手术，你的兄弟因在商场扒窃被捕。还有一件事儿挺重要，你好想干死那个便利店的红发小子，就是带着眼罩长着马蹄内翻足的那个。你可以为这些事儿祈祷吗？我认为你可以。 我说，你可以为你想要的一切祈祷， 为一切祈祷。但那个神的计划怎么办？<br />
<br />
??记得吗？神的计划。很久以前，上帝所创造的神的计划。深思熟虑，确定那是一个好计划，付诸于实践。而十亿多年来，神之计划就已经这样凑合进行着。现在，你过来祈求自己的东西。难道这东西不在神的计划之中？你要什么他做什么？改变他的计划？为你？这是不是有点儿傲慢？这可是一个神之计划。如果可以因为每个拿着两块钱的祈祷手册的王八蛋而随意搅乱计划，那还叫什么神？<br />
<br />
??而你还可能会遇到这个问题：假如，你的祈祷没有得到回应。你会说什么？“嗯，这是天意。” “你终将得到伸张。” 好吧，但如果是天意，神总会做他想做的事，那你还费什么劳什子祈祷啊？在我看来简直是大大地浪费时间！你就不能跳过祈祷这个环节服从他的意志嘛？真是太奇怪了。<br />
<br />
??综上所述，我决定崇拜太阳。但是，正如我所说，我不向太阳祈祷。知道我向谁祈祷？乔.派西（Joe Pesci）（注一）。原因有二：首先，我认为他是一个好演员，OK？对于我来说，这很重要。其次，他看起来像是个能办事而的人。乔不做操蛋的事儿。事实上，乔能解决某些上帝解决不了的麻烦。<br />
<br />
??我向上帝祈祷多年抱怨邻家的恶狗， 没用。 而乔拜访了那厮一次， 就搞定了。 你有时真惊讶棒球棒能完成这么多的任务。<br />
??所以我已经向乔约莫一年了。而且我也注意到了。我注意到我向上帝和向乔做的所有祈祷，都有着相同的回复率：百分之五十。一半一半。50比50。和幸运草，马蹄铁，美好的愿望，兔子脚（注二），和通过挤压公羊睾丸预测未来的伏都教女巫一样：50比50。因此，随便挑上一个什么迷信，坐下来，许个愿，乐呵去吧。<br />
<br />
??而对那些期望从圣经获得道德教训和文学修养的家伙，我可以向你推荐其他故事。您或许想要看看《三只小猪》，这是个好故事。有个不错的结局，你会喜欢这一点。还有《小红帽》，虽然它有限制级因素，大坏狼吃掉了祖母，而我也觉得无所谓。最后顺便说一句，我也常从《蛋头之歌》（Humpty Dumpty）得到大量的人文关怀。我最喜欢的部分？“全部国王的人，全部国王的马，也无法把蛋头凑回去。” 因为本没有蛋头，也本没有上帝。没有，一个也没，没有上帝，永远不会有。<br />
<br />
??事实上，我要这样说。如果有上帝，就让那个观众中风而死！瞧见了没？什么也没有。什么事儿都没吧？每个人都好？好吧，我和你说，我会加一点诚意。如果有上帝，就让我中风而死。瞧见没？什么事情都没有发生，哦，等等，我的腿有点儿抽筋，我也有点儿蛋疼。另外，我失明了，我失明了，噢，现在我好了，一定是乔.派西，啊是？上帝保佑乔.派西。非常感谢大家。乔保佑你！<br />
