													<h2>韩寒谈杭州富家子弟“飞车门”</h2>
						
						
																											<div id="postmessage_90501" class="t_msgfont">2009年05月11日(2009-05-11 03:04:01)标签：杂谈&nbsp; &nbsp; <br />
最近杭州发生了一起影响很大的交通事故。朋友说，你对汽车那么了解，有没有什么要说说的。<br />
<br />
我想起了前几天上海车展的时候，某视频网站给我做了一个小的访问，我当时说，开车最重要的就是要注意安全，真正的交通规则的宗旨其实就是安全驾驶和不妨碍到别人，每个人都会有超速开快车的时候，还有一些地方有为了征收罚款而设置的不合理限速，一个驾驶员一辈子没有超速过没有开过快车不大可能，这太窝囊了，但最关键是一定要注意安全。<br />
<br />
我一段话说了三次安全驾驶，结果视频出来以后，标题是“韩寒，表示没开过快车的人很窝囊”。这说明现在的视频网站的竞争真的很激烈，不惜以下次再也采访不到嘉宾为代价，也要眼前的点击率。<br />
<br />
我承认我有时超速，一些路况和视线极好的四车道国道，突然间会出现一个40的限速，我相信如果大家不刹那一脚车，百分之九十九的人都是超速的，但是，在城区，市区，社区等人多的地方一定要遵守限速。一些路况好的地方的低限速往往是为了相关部门创收。在这个事故中，我们不能矫枉过正，要求全国都限速40，交通有两个重点，第一是安全，第二是效率。<br />
<br />
在杭州这起事故里，很明显的，肇事者超速了，这条道路的限速是50，我认为这个地段这个限速是合理的，交警部门说，他当时的速度是70，我个人不是很认可这个速度的认定。当然，街上目测者的叙述只有参考价值，不能完全取信，但是，如果是70的速度，在这样的马路，视觉上是不会让人产生“快”的感觉的。这台车原厂的刹车配备非常的顶级，是准赛用的级别，100公里的速度刹车到0大概需要35米，车辆撞到人以后过了50多米才停住，我们假设他当时全力刹车，那速度应该是在每小时120公里左右的。<br />
<br />
但是，客观的说，目击者所说的150公里我认为可能是偏快的，因为街道参照物的关系，120公里每小时的速度会让人感觉更快，而且150公里以上的速度撞到人很可能不能保持肢体的完整。不过，从比较专业的角度，我是不认同交警给出的70公里没小时的说法的，我有一个朋友在类似的速度不小心撞到过人，对方只是轻伤。我个人的判断是100到130公里以内的一个速度。<br />
<br />
 <br />
<br />
街道飙车是非常危险的，但随着汽车行业的发达，富二代的增多，年轻富翁的产生，汽车改装和街道飙车一定不可避免会出现，任何国家都是这样的。但是所谓盗亦有道，一般来说，我认为最大的尺度就是在封闭的环线上偶然玩那么一下，以安全和可控为前提，以出了事承担所有责任为条件，要完全杜绝是不可能的，不光光是改装车，公交车，出租车，私家车都有互相斗气哦时候，很多特权车也特别喜欢趁领导不在飙车，但是，在闹市飞街的确是非常危险和极其错误的一件事情。一般来说，开快车分四种，一个是赛道，一个是空地，一个是山路，一个是环线，一个是闹市街道，除了第一二个是比较健康的以外，后三者的受鄙视程度和业余程度是递增的。尤其是最后一个，山路往往是单车事故，环线往往是多车事故，但街上就是人车事故了，但往往我们国内的汽车爱好者比较喜欢在街上开快车，因为街上人多，人多看的人就更多。这是最该谴责的。<br />
<br />
我认为，这起事故的关键并不是所谓的富家子弟和老百姓等阶级对立面的问题，虽然这个对立面的话题性和煽动性都比较强。这件事情重要的是肇事者和其朋友表现出来的个人素质以及交警部门的奇怪认定，而更重要的是，大部分的年轻人，男孩，都是喜欢速度的，我身边很多同学，平时很斯文，但买了车以后很多都来问我怎么样才能开快车，怎么样才能漂移，我给的答案只有一个，上赛道，练。让壮年男子踏实开慢车是不可能的，要把他们引导向安全的地方发泄荷尔蒙，比如赛道，如果你在赛道上开的越好，在街上开快车的欲望也越小，对车辆的控制也更好，对于在街道上处理紧急情况也更有帮助，而且，赛道上快是真的快，街上快是小儿科，有很多号称街道高手的人，到我们车队试训，慢的就像路边的石头，所以，我希望喜欢开快车的人都上赛道来，租用赛道不贵的，比洗桑拿便宜多了。<br />
<br />
逝者谭卓在最美好的时光里被交通事故夺取生命，他正准备结婚，我最了解享受爱情是多么美好的事情，可惜好风光似幻似虚，我们只能愿死者安息。<br />
<br />
关于肇事者，我到今天才知道，我原来和他还有一面之交，半年前在杭州的一个卡丁车场里，我一个朋友举行过一个十几人的业余友谊赛，当时他代表杭州开卡丁车比较好的本地的选手参加了这个友谊赛，排位赛的时候我是第一，正赛一起步就直接被他撞出去了，但后来我也没有计较，一来这又不是正经比赛，是业余的娱乐，二来因为他很年轻，总会冲动和犯错。这件事情，千错万错一定是肇事者的错，因为他的错，一个优秀的年轻人已经失去了生命，这个错无可弥补，但人生还有很多好事他出来以后可以去做，希望他可以明白。<br />
<br />
