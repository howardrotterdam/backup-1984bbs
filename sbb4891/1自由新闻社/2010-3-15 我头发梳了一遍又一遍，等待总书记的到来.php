　　“中午吃饭后，我头发梳了一遍又一遍，领带和衣服整理了又整理，就是为了有一个好精神，等待总书记的到来。”陈燕萍代表兴致勃勃地说道。下午三点整，总书记来到人民大会堂西大厅，参加江苏代表团审议政府工作报告。在认真听取9位代表发言后，总书记说：“今天，我是作为一名普通代表参加审议政府工作报告的。”总书记的话语，让代表们倍感亲切。整个讨论会生动活泼、气氛热烈，欢声笑语不断。<br />
<br />
