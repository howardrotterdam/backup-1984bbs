&nbsp; &nbsp; 见胡主席走过来，陈国韬代表敬礼报告：“我是北京军区某部一营营长……”话未说完，胡主席就亲切地说：“我知道。去年春节前，我去过你们部队慰问官兵，看过你们的训练课目演练，很精彩。”<br />
<br />
&nbsp; &nbsp; “谢谢主席，能再次受到主席接见是我一生的荣耀。”陈国韬代表激动地说，“请主席放心，我们一定会把部队建设成为一支能够经得起任何考验的‘拳头’和‘尖刀’部队。”<br />
