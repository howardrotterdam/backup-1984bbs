　　最后陈述仍不忘****<br />
<br />
　　在最后陈述阶段，黎强抓紧最后的机会为自己求情，并感谢法庭、法警的关照：“这六天的庭审，对我来说也是一场法制教育……公诉人这几天也辛苦了，你们到渝强公司去复印了那么多箱的材料。希望法官能公正判决，免得海外媒体报道。如果判决合情合理，我就不****了。”黎强的最后陈述，让庭上庭下一阵叹息哄笑。<br />
<br />
　　黎氏语录<br />
<br />
　　●我说不出来，我希望新闻单位来采访我。<br />
<br />
　　●我是个不懂法的人但我守规矩，我承认会打擦边球，我只是一名普通商人。<br />
<br />
　　●说要解放思想，我就动了脑筋，将旧车置换后再买新车，不仅没扰乱秩序，还方便了群众，带来了税收……这样说不该定我的罪，还应该表扬我。<br />
<br />
　　●就算枪毙我也该让我看看，上路的时候我也明白。（黎强对违法所得的计算方式提出质疑）<br />
<br />
　　黎强律师“授课”迷倒众人<br />
<br />
　　这位“临危受命”的博导质疑：1849件证据无一指证黑社会<br />
<br />
　　昨日，黎强的辩护律师――来自西南政法大学的刑法学老专家赵长青发表辩论意见，否认黎强团伙涉黑。老教授不急不缓，从法条到法理，由浅入深对罪名逐项解释推证，生动释义。在其发言的一个多小时里，整个法庭鸦雀无声，包括法官在内的庭内众人全都听得“如痴如醉”。很多记者都感叹：“讲得太好了，又受了一次法学教育。”<br />
<br />
　　据悉，赵长青是西南政法大学的博士生导师，是当地知名刑法学领头人，当地政法系统不少人出自其门下。据闻，赵长青久未出山，此次受黎强家属所托半路“临危受命”，在开庭前十多天才接下代理。<br />
<br />
　　起诉语言太概括性<br />
<br />
　　赵长青指出，起诉书语言以概括性的论述为多，比如说“称霸一方，残害群众”、“组织架构稳定”。但对于这些论述，只具体举出了两件单独的黑社会性质组织犯罪事实，其中一件是去年的“11・3”出租车罢运事件，但公诉机关随后没有举证指控这一件事。<br />
<br />
　　200万补偿仅是说说<br />
<br />
　　对于指控“共创公司”为黎强以非法手段企图控制客运市场的具体表现，赵长青认为，成立公司此事本身不能说明问题，因为扩大规模、扩大占有率，这是任何一个企业的本能。<br />
<br />
　　至于该公司的章程里被指控有“如公司董事因执行公司事务、执行董事会决议的行为被公安机关追究刑事责任或被拘留，则其他董事每人补助被追究者人民币200万元作补偿”的说法，他说，如果章程里确实有这段，那确实是个问题，但法庭上查证据，却指“共创公司”没把这个搞起来，这仅仅是个说法，并没有形成规章。<br />
<br />
　　据此，赵长青指出，把这两条作为指控“黑社会性质组织”的事实显然不够。<br />
<br />
　　证据无一证明黑社会<br />
<br />
　　赵长青指出，公诉机关共有1849件证据，但没有一组证据是证明黎强组织、领导黑社会性质组织罪的，关于其如何组织、领导的证据也没有。如起诉书上只列举了公司成立的证据，如在工商登记成立了几家公司等，但公司成立并不是组织、领导黑社会性质组织的证据。<br />
<br />
　　此外赵长青还发现，在黎强的17次口供中，公安机关没有问过一次“你是怎么组织的黑社会性质组织”、“你是怎么领导的黑社会性质组织”。<br />
<br />
　　论证应该“证据说话”<br />
<br />
　　此外，赵长青还指出公诉机关在起诉黑社会性质组织犯罪上有矛盾的地方。起诉书上说黎强组织、领导黑社会性质组织的方式是采取公司化的管理模式，牢牢掌握了渝强公司的人事权、财产权、经营决策权，这说明黎强公司就是个黑社会性质组织，“但家族式的民营企业显然也是这样的。”赵长青说。<br />
<br />
　　赵长青还指出，起诉书上指出有另一种论证，指控黎强成立的“组织”里面的副总、科长之类的都是黑社会性质组织成员。他认为，如果认为黎强公司本身就是黑社会性质组织，那当然认为什么都是黑社会。但必须先有充分的证据证明这个黑社会性质组织是怎样搞起来的，现有的证据不充分，需要补充证据。<br />
<br />
　　不过赵长青承认了黎强有扰乱交通秩序、非法经营、逃税、行贿的行为。</div>

							
							
							
							
