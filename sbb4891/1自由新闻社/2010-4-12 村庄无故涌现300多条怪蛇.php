													<h2>村庄无故涌现300多条怪蛇</h2>
						
						
																											<div id="postmessage_559568" class="t_msgfont"><img src="http://img1.gtimg.com/news/pics/31722/31722857.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
