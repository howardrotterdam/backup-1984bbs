<br />
短短一天半内，村口竟无故钻出300多条从未见过的怪蛇！其头形似三角，恐有剧毒。全村人惊惶不安，见蛇就打，却屡打不绝。目前，警方及林业局人员已将蛇拿去化验，结果未明。<br />
<br />
凌晨出现 布满泥路<br />
<br />
10日晚，增城村民郭生报料称，增城中新镇福和官塘村柏木路12号的泥路上遍地都是蛇，令人眼花缭乱，还不断有新蛇出现，怎么打都打不完。<br />
<br />
据了解，这些蛇并非一夜出现。10日凌晨3时，当地村民郭景到村口取钥匙，猛然看到自家窗台下爬着近10条一模一样的蛇，都是三角脑袋，顿时大惊失色，连忙拿了棍子朝地上一顿猛打。天亮后，郭家几名女眷外出经过堆满柴草的草屋时，又发现了十几条这种蛇，连草屋里都有，忙喊人来打。谁料这种蛇越打越多，中午时分，郭家祖屋周围竟一下子又出现了50多条。村民们颇感蹊跷，也越来越害怕。<br />
<br />
“村里七八十岁的老人家都说从没见过这种蛇，周围石缝杂草很多，不知道是从哪里钻出来的。听说有两三条跑到了邻居家里，幸亏没伤到人。”郭生说。为了不让蛇进屋咬人，村民们纷纷在屋外撒下硫黄。<br />
<br />
活捉几条 进行化验<br />
<br />
可到了傍晚，这些蛇不但没有消失的迹象，反而越发猖狂。<br />
<br />
据郭生形容，在他家后面的老泥路上，突然间群蛇出动，密密麻麻布满了路面，令人眼花缭乱，粗略估算有200条！这下，全村人都抄起家伙赶了过来，和郭家兄弟一起开始了人蛇大战。可这边刚死一条，那边又有新的冒出，村民们无奈下只得报警。<br />
<br />
当晚至昨日下午，接连有两批警察和林业局人员赶到现场帮助清理，并活捉了几条带往市区化验。<br />
<br />
据初步统计，截至昨日傍晚，该村共出现了300多条这种蛇，几乎全被打死，但仍有少量蛇窜出。<br />
<br />
非本地蛇 不怕人打<br />
<br />
当地人皆称从未见过这种蛇，并非本地蛇种。据介绍，这些蛇属于同一种类，身长30厘米左右，手指粗，树皮色，有黑灰色斑点。头是三角形，看上去有剧毒。“这些蛇很怪，别的蛇，尤其是野生的，一般见人打就跑，但这些蛇就盘在那里一动不动地给人打，懒洋洋的攻击性也不强。”郭生颇感不解。<br />
<br />
<br />
■说法<br />
<br />
初步鉴定为短尾蝮蛇<br />
<br />
该蛇毒性较烈，但分布广泛，抗毒药品易得<br />
<br />
村民：有人故意放蛇报复？<br />
<br />
郭景说，蛇出现比较集中的地方主要在他和几个本家聚居的石角社周围一带，他这一大家人有几个在村里任过职，他的妻子是村妇女主任，主管计划生育，一个叔叔曾担任村治保主任，他现在又是石角社的社长，“工作中难免得罪人，也许有人用这种方式报复？”<br />
<br />
郭景怀疑村里出现的蛇是有人故意放的，目的是报复他一大家子。<br />
<br />
专家：可能是蛇聚居交配场合<br />
<br />
华南濒危动物研究所胡慧建博士说，正常情况下，一个地方同时出现大量的蛇主要有三个原因，一是发现蛇的地方本来就是蛇的聚居区，现在天气转暖，原本冬眠的蛇开始出来活动；二是由于最近下雨，雨天气压相对较低，蛇在地下比较闷，所以雨天蛇一般都会跑到地面上来。三是现在正是各种动物发情交配的季节，大量的蛇出现在同一地区，很有可能是要繁衍后代。<br />
<br />
胡慧建博士认为，这样大规模出现蛇群是自然现象，最大的可能就是当地本来就是蛇的聚居区和交配场合，与其他诸如地质灾害、地震前兆等没关系。预计这些蛇完成交配之后，会返回各自的生存区域。<br />
<br />
昨晚9时15分，胡慧建博士根据记者提供的图片初步鉴定，官塘村出现的数百条蛇是短尾蝮蛇，具体品种有待现场进一步鉴定。<br />
<br />
林业局：暂未有村民遭蛇咬<br />
<br />
昨日下午，增城市林业局的工作人员随同当地派出所民警赶到官塘村实地考察。昨晚，该市林业局办公室一名负责人说，暂时还不清楚大量蛇为何突然出现在官塘村，这种蛇是什么蛇、是否有毒性还有待调查。<br />
<br />
目前，还未发现有村民被蛇咬；今日，将请华南濒危动物研究所的专家到官塘村实地调查。<br />
