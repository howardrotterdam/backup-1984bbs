“我们不能再犯这么多错误了。”在赛后的新闻发布会上，马拉多纳称赞了获胜的价值，但也批评了球队在临门一脚上的欠佳，他提醒球员说，足球规则是无情的， “当你犯下很多错误，那最后平局也完全有可能，感谢上帝，没有给他们(平局)。”<br />
<br />
　　总喜欢张开大嘴对国际问题和各国政要发表生猛的观点，是马哥显著的特点。虽然他的政治观幼稚、经常自相矛盾，但他&quot;张嘴必有料&quot;的特性还是一种巨大的勇气。在纪录片《马拉多纳》中，马哥的&quot;政论&quot;是一个要害部位，他用超乎寻常的气魄发动了一次对美国的口水总攻......<br />
<br />
　　马哥切齿痛恨美国始于1994年世界杯在波士顿药检不合格，此后他连续三次申请美国签证均遭拒绝更是火上浇油，毕竟阿根廷是整个拉丁美洲惟一一个免签证的国家，美国拒绝他让马哥感到歧视和羞辱。<br />
<br />
　　在整部纪录片中，马哥多次穿上一件印有&quot;布什，垃圾&quot;字样的反美T恤衫，从1994年到今天，马哥一直在旗帜鲜明、口无遮拦地反美，在纪录片中，马哥说：&quot;布什，是什么?他就是世界头号杀人犯!&quot;何出此言?&quot;你可以看到他拥有很多炸弹，他拥有的炸弹可以炸掉半个地球，他每年贩卖的炸弹也可以炸掉半个地球!&quot;在马哥眼中，美国总统这个位置从来就没有拥有过真正的权力，马哥说：&quot;布什看上去拥有一切，但一个政治家是否伟大，从来就不是看他拥有什么，权力大小不是靠拥有多少来决定的。&quot;<br />
<br />
　　&quot;他如果没有成为一名足球运动员，一定会成为一个革命者，一个内心革命者。&quot;导演库斯图里卡如此评价他眼中的马哥。马哥反美也曾摆过一次&quot;乌龙&quot;---他和阿根廷前总统梅内姆感情极好，但梅内姆是近三十年来阿根廷历任总统最&quot;亲美&quot; 的，2000年梅内姆卸任时，有记者问马哥：&quot;你怎么评价这条美国走狗?&quot;马哥罕见语塞，他只能搪塞一句：&quot;我和梅内姆只谈足球，我们从来不谈美国。&quot;<br />
<br />
　　全球只有中国不是美国殖民地<br />
<br />
　　1996年夏天马哥随博卡青年队访华，他当时在昆仑饭店吃了一顿北京烤鸭，马哥当场就对陪同他的中国朋友说：&quot;你们有这么好吃的东西，不要去引进那些美国快餐，一个国家应该捍卫自己的传统。&quot;此后马哥在参观故宫时又发感言：&quot;这是全世界最伟大的一座博物馆，我希望那些曾经抢夺过这座博物馆的强盗们归还属于中国人民的艺术品!&quot;<br />
<br />
　　反美，在马哥看来关键是&quot;反美国文化的垄断侵略&quot;。马哥说：&quot;加西亚-马尔克斯曾告诉我，如果不是卡斯特罗出现在拉丁美洲历史上，那么这片土地上的所有小孩现在都说着英语，所有男人都在为纽约扬基队尖叫。&quot;马哥平时对两个女儿就特别注重&quot;意识形态&quot;控制，他不准女儿去看《蜘蛛侠》和《指环王》，马哥告诉她们：&quot;在你们的爸爸面前，那些美国怪物都不算什么英雄!&quot;<br />
<br />
　　在纪录片中，马哥表现出一种深度的忧虑正是对美国商业文化无孔不入感到担心。包括他热爱的古巴，马哥说：&quot;最近十年，古巴也有变化，好莱坞电影和 NBA录像在古巴人的生活中占据了越来越多的时间。&quot;最后，一直将毛泽东视为&quot;东方巨人&quot;的马哥非常严肃地说：&quot;在我眼中，全球只有中国不是美国的殖民地，历史上不是，以后也不会是!&quot;</div>

							
							
							
							
