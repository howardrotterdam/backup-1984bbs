<br />
29岁市长之妻职务及家庭信息披露:父亲现无业<br />
<a href="http://www.sina.com.cn" target="_blank">http://www.sina.com.cn</a>&nbsp;&nbsp;2009年06月23日22:18&nbsp;&nbsp;中国新闻网 <br />
　　中新网武汉6月23日电 (记者 艾启平) 6月21日下午，湖北宜城市四届人大四次会议以无记名投票的方式，选举产生了新一任人民政府市长，今年29岁的周森锋以全票当选。此消息经有关媒体报道后，立即在网络上引起巨大反响，各种赞美和质疑的声音让这位新上任的年轻官员倍感压力。<br />
<br />
　　来自襄樊市官方消息称，襄樊和清华大学签订有人才与科技全面合作协议书。作为襄樊从清华引进的高素质人才，周森锋2004年从清华大学管理科学与工程专业毕业后到襄樊挂职，此后历任襄樊市建设委员会副主任、襄樊市城市规划管理局副局长、宜城市委副书记、政府副市长等职。官方还澄清，周森锋来自农村，没有任何家庭背景。<br />
<br />
　　宜城是一个县级市，属于襄樊市管辖。周森锋在当选后也表示，他一定不负重托，不辱使命，恪尽职守，把全部精力贡献给宜城。<br />
<br />
　　另据有关媒体报道：“周森锋与爱人是高中同学，同为禹州市人，大学也是同窗。两人大学毕业后，又一同考入清华大学攻读硕士学位。2004年，周森锋被湖北省襄樊市以高素质人才从清华大学引进到当地任职。不久，他的爱人也被襄樊市录用，目前，在襄樊市人大常委会担任副主任。”<br />
<br />
　　针对上述报道，记者23日从襄樊市权威部门获得了周森锋妻子的有关信息：霍焰，1980年6月出生。其父亲霍文彬，54岁，高中，中共党员，原为河南省禹州市神后镇下白峪煤矿会计，该煤矿整体转让后，现无职业；其母亲李焕亭，54岁，小学肄业，在家务农。霍焰1999年9月考入西南政法大学法学专业读本科，2003年9月至2006年7月在清华大学法学院诉讼法学专业学习，取得硕士研究生学历、法学硕士学位。2002年5月加入中国共产党。<br />
<br />
　　据了解，2006年9月，霍焰作为优秀人才被襄樊市引进到市中级人民法院工作，2006年12月任襄樊市中级人民法院团委副书记。2008年7月，霍焰参加了襄樊市面向湖北省的35岁以下副县级年轻干部公开选拔，通过资格审查、笔试、面试等环节，霍焰在竞争中脱颖而出。经过组织考察、按1：3的差额比例提请市委常委会票决、按1：2的差额比例提请市委全委会票决、上报有关部门审批等程序，市委研究提名霍焰为襄樊市人大常委会内务司法工作委员会副主任人选。<br />
<br />
　　按照法律程序，2008年11月7日，襄樊市第十五届人大常委会第六次会议任命霍焰为襄樊市人大常委会内务司法工作委员会副主任。同时，中共襄樊市委决定，对公选出来的年轻干部均实行一年的考察期，霍焰现仍被集中安排在市信访局信访专班实践锻炼。干部职工反映，霍焰为人低调、工作踏实、品行端正。(完)</div>

							
							
							
							
