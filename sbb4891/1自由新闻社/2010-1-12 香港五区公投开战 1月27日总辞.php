<br />
对于两党所说的「公投」，大陆国务院港澳事务办公室已发表声明，强调香港依法没有「公投」；香港行政长官曾荫权也说，如果议员辞职，当局只是依法补选，并非「公投」。<br />
<br />
自由党代表香港工商界的利益，虽然在某些问题上与政府若即若离，但一般都被视为较亲政府的政党。<br />
