<br />
田北俊重申拟出战新界东<br />
<br />
多次表达有意「去马」参与补选的自由党前主席田北俊表示，正与建制派协调，重申自己一旦出选，会再战新界东；其弟田北辰有可能出选新界西或九龙西，但不会考虑转战港岛。</div>

							
							
							
							
