													<h2>余若薇s公投如何挑战法律？</h2>
						
						
																											<div id="postmessage_443608" class="t_msgfont">明报网上版 2010-01-21<br />
--------------------------------------------------------------------------------<br />
余若薇s公投如何挑战法律？ <br />
<br />
【明报专讯】公民党与社民连宣布推动「5区公投运动」后，国务院港澳办公室罕有地高调「表示严重关注」，指这个运动「没有宪制性法律依据，没有法律效力」，「与香港特别行政区的法律地位不符，是从根本上违背香港特别行政区《基本法》和全国人大常委会有关决定的」。<br />
<br />
根据香港法律，凡法例没有禁止的，便是合法。而是否有法律效力，与是否违法，是两码子事。2003年50万人上街，促使特区政府撤回了《基本法》第23条的立法建议；游行没有法律效力，但游行并不违法。《基本法》附件一、二说明修改行政长官与立法会选举办法的法定程序，没有提到公投；公投无法律效力，但公投并不违法。 <br />
<br />
究竟公投运动违反了宪法或法例哪个条文哪个章节？至今未有人清楚说明。 <br />
<br />
「5区公投运动」「为民主而来」<br />
<br />
「5区公投运动」的议题是「尽快实现真普选，废除功能组别」，若问这公投「为何而来？」，简单答复是「为民主而来」，为实现《基本法》第45条及68条香港最终达至普选的目标。 <br />
<br />
许多地方有公投制度，例如冰岛、瑞士、美国，奥地利、丹麦、爱尔兰、匈牙利、保加利亚、荷兰、挪威、意大利、波兰、葡萄牙，交由全民投票，处理具争议性的项目。提案者是官方或人民、提案人数、投票率等等规则，各地不同，没有划一标准。 <br />
<br />
去年底，瑞士就是否禁止修建清真寺尖塔（宣礼塔）举行全民公投，结果57.5%选民投反对票，政府遵照公投结果，不准建新的宣礼塔，现有4座不受影响。因金融风暴而负债累累的冰岛拟于今年3月举行公投，决定与英国、荷兰的还债条款。 <br />
<br />
其实，20年前《基本法》起草委员也曾采取开放态度，视公投为一种具法律效力的收集民意方式，藉以决定香港的政制改革，而非把公投定性为抗争、作反、违法。1989年2月21日公布的《基本法》第2份草稿中，附件一如下s「第三任行政长官的任内，立法会可以拟订具体办法，通过香港特别行政区全体选民投票，以决定是否由一个广泛代表性的提名委员会，按民主程序提名后，普选产生行政长官……」附件二s「在第四届立法会的任内，立法会拟订具体办法，通过香港特别行政区全体选民投票，以决定立法会议员是否应全部由普选产生……」 <br />
<br />
当时附件一、二草稿仔细订明公投的条件和法律效力s「上述全体选民投票的举行，必须获得立法会议员多数的通过，征得行政长官同意及全国人大常务委员会的批准，方可进行，投票结果，必须有百分之三十以上的合法选民的赞成，方为有效，付诸实施。」 <br />
<br />
公投并非大逆不道 洪水猛兽<br />
<br />
历史证明，公投并非大逆不道，洪水猛兽。后来《基本法》定稿剔除了公投内容，不是因为它违法违宪。 <br />
<br />
奇怪的是，特区政府一直强调「5区公投运动」只是补选，不承认它是具法律效力的公投，也就是一场只属于香港内部事务的补选，国务院港澳办的「严重关注」与特区政府的故作淡然自相矛盾。 <br />
<br />
特区政府施政专横，香港人无力感日增，最近不少民意调查结果显示，大部分市民即使不满政府提出的政改方案，但无奈地接受。公民党希望尽最大努力，推动变相公投，将政制改革的选择权归还市民，以一种和平方式，透过票箱，量化民意，向当权者表达对实现真普选与废除功能组别的决心。 <br />
<br />
在有限的鸟笼空间，竭尽所能争取最大的公道，不就是香港人最擅长的吗？<br />
<br />
