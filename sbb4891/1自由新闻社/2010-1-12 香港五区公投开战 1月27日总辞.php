 1月 27日总辞<br />
五区公投开战<br />
<br />
2010年01月12日 (07:45 am) <br />
<br />
《争普选　展开民主运动新一页》<br />
<br />
公民党及社民连昨日召开联合记者会，正式宣布两党就五区公投运动达成全面共识，并签订合作备忘录。根据备忘录，两党会成立「五区公投联合委员会」，由公民党党魁余若薇出任召集人；公民党秘书长陈家洛及社民连副主席陶君行担任副发言人，全面统筹五区公投各项动员、拉票、宣传工作，初步预计公投开支 300万至 400万元。<br />
<br />
两党同意一致行动，本月 27日派出五名立法会议员辞职，正式开展运动，参加补选的名单本月稍后公布。按以往惯例，议员辞职后，政府会先刊宪公布五个立法会议席出缺，然后再刊宪公布五区进行补选，以及补选提名期及选举日期。以上次港岛区为去世的民建联前主席马力议席，安排补选时间计算，今次由议席出缺到补选进行，估计要三到四个月，即最迟 5月底进行公投。<br />
<br />
公投的议题为「尽快实现真普选，废除功能组别」，两党认为，特首选举提名应不经筛选，由香港人一人一票普选产生，立法会议席所有议席由直选产生，以及香港应于 2012年实现双普选，若未能达致，中央政府应立即向港人承诺不迟于 2017年普选特首，及不迟于 2020年普选立法会。<br />
<br />
社民连副主席陶君行指出，五区公投需全港市民支持方能成功，必须泛民全体合作，所以他们已搁置早前和其他民主派就五区公投的争议，希望一同打开香港民主运动新一页，「过去已经过去，我O&#60811;家要展开民主运动新一页」，认为现时已到达本港民主发展的关键时刻，有需要让市民透过一人一票直接民主，表达对香港民主发展的看法，「我O对香港人有信心，对香港人对争取民主有信心。」<br />
<br />
公民党秘书长陈家洛也说，五区公投是一次新民主运动，让全港市民就本港民主发展直接表态，为现时的政制死局带来新希望、新改变，「呢个系最公道、最客观机会，每个香港人都有一票，直接表达要求尽快双普选、取消功能组别的诉求。」<br />
<br />
不过，两党承认今次公投运动存有风险，泛民有可能落败，所以陈家洛不排除两党因公投落败，要支持港府政改方案的可能性，但他希望市民集中票源支持泛民推荐的候选人，与保皇派的候选人进行对决，「我O期望制造一个对决的结果，民主派五位成员对非泛民主派五位成员，就我O出议题，清脆利落、切切实实畋缏郏然后畀大家投票。」<br />
<br />
两党将成立「支援新民主运动委员会」，让社会不同界别和全港市民支持运动，又会于总辞当日，在中环遮打花园举行千人起动大会，呼吁市民到场支持。据悉民主党创党主席李柱铭、前政务司司长陈方安生已表明支持五区公投，为他们拉票，稍后他们再联络其他泛民成员，希望他们加入动员行列。<br />
<br />
政制及内地事务局局长林瑞麟回应五区公投时重申，香港没有公投的机制，任何重大政制安排，都要先经立法会通过，由港府提案，再交人大常委会备案，又说两党行动不会影响政府提交政改方案的时间。</div>

							
							
							
							
