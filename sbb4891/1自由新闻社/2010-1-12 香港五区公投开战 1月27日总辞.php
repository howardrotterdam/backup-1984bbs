<br />
　　香港泛民主派立法会议员企图用辞职后再参选来达到变相公投的目标可能受到亲中阵营的弃选打击，声称正积极考虑参选的自由党突然宣布不会参与补选，而泛民阵营中与中方关系较好的民主民生协进会亦决定，不会支持推动变相公投。<br />
<br />
　　另一表明不参与辞职后补选的泛民主派立法会议员梁耀宗更公开要求公民党和社民连取消五区总辞，他指出，以「起义」来宣传五区辞职有流血及冲突的意思，并不适当，而自由党已决定不参加补选，如果民建联亦弃选，自会影响投票率，失去市民踊跃参与投票的意义。有关言论令泛民议员的内部不统一更形对立。<br />
<br />
　　曾在零八年选举中败阵的自由党常委田北辰早已加强在地区派发宣传单张，及至周六早上仍然表示有意出选，但要取决于党的意愿。不过，自由党常委傍晚会召开紧急会议，决定不会参加五区请辞后的补选。<br />
<br />
　　该党主席刘健仪解释两日内改变立场时指出，公民党及社民连提出的「全民起义」，言词激烈，一再挑战一国两制的底线，相信理性的市民不会支持，而且「起义」有反动及推翻政府的意味，自由党不能接受，恐怕一旦参与补选，会被外界扭曲为自由党赞同起义，故此不会派人参选。<br />
<br />
　　民主民生协进会周六晚亦召开中常委会议，通过不会协助推动社民连及公民党发起的五区请辞行动，不会具名支持亦不会为两党站台。<br />
<br />
　　另一亲建制政党民建联和工联会则表示，仍在研究局势，未决定是否参与补选。<br />
<br />
　　时事评论员林和立曾表示，中方不会希望亲中政党参与变相公投的普选，以免刺激投票率升高。有关政党的转变，显然中方已向有关政党传达立场。<br />
<br />
　　另一时事评论员刘锐绍则有不同预测，他认为，北京方面和建制派高调反对五区请辞和抨击运动用「起义」的字眼，可能造成民意反弹，令市民反思中方为何如此大反应。<br />
<br />
　　发起变相公投的副发言人陶君行以「落荒而逃」来形容建制派的弃选，指当中国政府要打压公投时，建制派就显得很恐惧，放弃为港人争取真普选。他又说，会继续推动变相公投，让市民表达争取普选的意愿。<br />
<br />
　　另一方面，建制派议员梁美芬明天(周一)会提交修订条例草案，限制议员随意辞职后一年内再次参选，由中国政府委任的基本法委员会委员谭惠珠赞成修例。<br />
<br />
　　身为资深大律师的五区请辞行动发言人余若薇反对为补选加设限制，认为应该由选民作决定。</div>

							
							
							
							
