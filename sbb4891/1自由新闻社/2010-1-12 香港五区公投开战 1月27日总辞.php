													<h2>本报委托港大民调s反高铁令政党全变输家 5议员下周二递请辞信</h2>
						
						
																											<div id="postmessage_445707" class="t_msgfont">明报网上版 2010-01-22<br />
--------------------------------------------------------------------------------<br />
本报委托港大民调s反高铁令政党全变输家 5议员下周二递请辞信 <br />
<br />
【明报专讯】「五区公投，全民起义」，酝酿多时的泛民5区请辞昨日终于落实，公民党和社民连5名代表下周二会向立法会递交请辞信，周五生效。运动以「全民起义」为口号，公投议题为「尽快实现真普选，废除功能组别」。本报委托港大进行的民调显示，泛民各政党及建制派在反高铁事件后，市民对他们的观感「变坏」比率，都高于「变好」，随时左右补选结果。<br />
<br />
一如所料，两党请辞名单包括港岛陈淑庄、九龙东梁家杰、九龙西黄毓民、新界东梁国雄及新界西陈伟业，5人下周二会向立法会秘书处递交辞职信，周五生效。但两党昨日未有实时公布参选名单，余若薇称，人选在现阶段并非最重要议题，他们亦未有讨论。两党下周三会在立法会发表辞职演辞及发起千人造势大会，出席者包括民主党元老李柱铭和部分泛民议员。<br />
<br />
斥「港独」质疑是上纲上线<br />
<br />
「补选公投」备受争议，社民连和公民党昨日举行联合记者会，不时受到传媒围攻，以下是部分节录：<br />
<br />
问：你们以「全民起义」为运动口号，是否有意「搞港独」？<br />
<br />
余若薇：「绝对不是 ……有人话搞港独，这是上纲上线，可能因为他们辞穷理屈，所以扣了一顶唔戴的帽。」<br />
<br />
问：如果选民不喜欢该候选人，你们会怎样做？（英译中）<br />
<br />
余若薇答：「如果（结果）不理想，可能是最后一次……人选不是重要，不是考虑最好或差人选，是投票给议题。」<br />
<br />
问s若运动失败，你们会如何问责？<br />
<br />
余若薇答s「建制派选到，支持政府的票会多了，现实是就算2012年想重新再来，市民可以话上次做得很错，不支持你。」<br />
<br />
建制派亦筹备反击，专业会议梁美芬表示会提出私人草案，禁议员辞职后再参选（见另稿）。前人大常委曾宪梓直斥提出「5区公投」的人「无脑」，又指泛民反中乱港，「搞乱整个社会对大家都没什么好处」。<br />
<br />
曾钰成s辞职演辞要经批准<br />
<br />
政制及内地事务局发言人重申「公投」欠缺法律效力，政府不予承认，亦不会影响2012两个选举办法的修改程序。对于5名辞职议员希望在下周三的立法会大会上各发言15分钟，立法会主席曾钰成表示该发言不可以引起辩论，他要待收到议员的发言稿后，才能决定是否批准。<br />
<br />
另外，本报在高铁拨款通过后两日，委托港大民意研究计划就市民对政党观感变化展开调查，以随机抽样形式电话访问548人。结果显示各大党都沦为输家，市民对各大党的观感「变坏」比率都高于「变好」，当中社民连更是大输家，市民观感「变坏」比率高达31％。<br />
<br />
80后撑公民党社民连输最多<br />
<br />
若只计算「80后」（21至29岁）受访者，他们在高铁事件后对建制派观感变坏的比率为26％。公民党则是泛民党派中，唯一争取到「80后」支持的（见图）。<br />
<br />
中大政治及行政学系高级导师蔡子强分析，更多市民对社民连「反感」，相信是因为高铁拨款期间示威者冲击立法会的行为，虽然并非由社民连动员，但亦令市民「入社民连数」。<br />
<br />
