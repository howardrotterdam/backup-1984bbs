<br />
梁美芬拟提案禁同届再参选<br />
<br />
曾经提出否决补选拨款阻挠5区请辞的梁美芬，昨日表示会提出私人条例草案，在《立法会条例》第39条「丧失获提名为候选人或当选为议员的资格的情况」中，加入规定丧失议员资格后，该人不可于同一届再参选。换言之议员自动请辞后，不得再参加补选。「目的是要禁止议员不能在无理由、无限制地辞职。」梁美芬表示，今日会把草拟本交予其它有兴趣的议员，并会寻求特首和立法会主席的批准。<br />
