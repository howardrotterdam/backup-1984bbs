													<h2>苹论：争民主下一站──五区公投</h2>
						
						
																											<div id="postmessage_426738" class="t_msgfont">Apple Daily 苹果日报网上版－－2010-01-12<br />
--------------------------------------------------------------------------------<br />
苹论：争民主下一站──五区公投<br />
<br />
经过几个月来反覆酝酿讨论，经过几轮激烈的争议后，主催五区公投的公民党、社民连终于敲定公投的细节及判定标准，稍后将会公布五位辞职立法会议员的人选。<br />
<br />
根据两党的构思，公投正式定名为「五区公投运动」，宗旨是「尽快实现真普选，废除功能组别」。至于公投的胜负则以五个选区总得票计算；只要五位候选人总得票超越五区主要对手的总得票，即使个别选区有候选人落败，公投议题仍然算成功通过。假若五区候选人总票数少于主要对手，公投便算失败，两党有可能因此而被迫支持政府提出的政改方案。两党发言人昨天又强调，今次五区公投运动不是某党派争取曝光或某些人要抢位，不是一场政治骚；而是一次可以让所有市民认真参与的新民主运动；两党会尽力争取其他泛民政团及市民的支持。<br />
<br />
从纠缠于骂战到推出具体的公投方案及行动细节，公民党、社民连及其他有心人付出了相当的努力。往下来两党除了定出辞职及参与补选的人选外，更重要的是争取其他民主派政团及人士支持行动，并向市民清楚解释公投投的是甚么及为何有必要。<br />
<br />
毫无疑问，公民党、社民连主催的五区公投运动不是一场游戏，不是一场政治骚。但是，香港的宪制毕竟没有公投或全民投票的机制，市民也没有参与公投的经验。因此，从现在到投票日为止，两党及支持公投的人必须尽力向市民、选民解释今次投票跟过往不同。今次五区投票不是要把某一位候选人送进立法会，不是要打败其他政党，而是要支持一项议题，而是要支持落实真普选及废除功能组别。只有让市民、选民清楚了解这个事实，今次选举才会真正变成公投，五区公投才真的有意义。<br />
<br />
另一方面，不少人包括一向支持民主运动的人对两党力推五区公投的策略有这样那样的保留及怀疑，明确支持的人仍不算多。这样的反应当然跟泛民内部前一阵子的争论有关，但主催公投的政党未能清楚解释公投的必要性也是重要因素。要扭转市民的冷淡及保留态度，两党不但要全力启动选举工程，更要让市民明白过往的策略成效不大，推进民主需要更直接、更有力的行动，需要市民用手上的一票发出更响亮的呼声。当市民了解到五区公投的用意就是让人民当家作主及争取政制发展的主动权时，他们便会踊跃参与，积极投票，令投票率超过五成的目标，令公投成为有广泛支持及参与的政治行动。<br />
<br />
建制派人士如郑耀棠之流批评公投运动浪费公帑，又指今次行动只是民主派自说自话，根本不是公投。这样的批评实在软弱无力。政制改革，落实双普选是香港的重要发展，是涉及全港七百万人福祉的事；政府不过付出有限公帑便能清楚了解市民的意向，便能明白真正的民意；这比花钱找一大堆顾问公司、研究机构做民意调查好得多，有效得多。<br />
<br />
至于公投的认受性从来不是由任何政党或政府说了算的，从来都是来自人民的。只要市民明白投票的意义，只要市民踊跃参与，由总辞启动的五区公投足以产生强大的道德及政治力量，令中央政府及特区政府不能不正视，不能不认真对待。<br />
<br />
