													<h2>张志刚s谁代表特区政府 谁代表北京中央</h2>
						
						
																											<div id="postmessage_430836" class="t_msgfont">明报网上版 2010.1.14<br />
--------------------------------------------------------------------------------<br />
张志刚s谁代表特区政府 谁代表北京中央 <br />
<br />
【明报专讯】社民连和公民党宣布，把5区呈辞正式定名为公投，议题是「尽快实现真普选，废除功能组别」。公社两党其实在提出其呈辞的建议时，已经附有公投之意，但到现时，则是白纸黑字的真正建议，以补选作为公投，并且连命题也拟定，那各有关方面就不能再含混其辞，视而不见，必须对事件严肃处理。<br />
<br />
首先是特区政府和北京政府如何面对这被包装成为公投的补选，其最关键的因素，是特区和北京政府如何看待公社联盟的所谓公投和议题的定性。如果视之为无关痛痒的自说自话，只是属于无任何正式地位的宣传或选举语言，则无论是公投也好，叫补选也好，特区政府和中央政府都以一般性补选而视之，那就选举照搞，但其政治意义就任君演绎。<br />
<br />
谁又可以代表议题另一面<br />
<br />
但如果特区和中央政府以非常认真和严谨的态度来对待公社联盟的正式宣言和提出的议题，那就很明显是有违《基本法》的规定。无中生有，非法僭建一个以补选为名的公投，到时特区政府就需要三思，是否要进行一场被定性为公投的补选。<br />
<br />
公社联盟的宣言和议题是何等身分和效力，是一个法律上的问题，那就由法律专家去解释并解决。但在政治层面，非公社联盟以外，尤其是被视为建制派的政党政团及个人，应否参与其事，则不必由法律专家去决定，而应该以整体政治得失而定其行止。<br />
<br />
公社联盟以补选代公投的玩意，其最大问题，就是无论如何代，也是无法代得进去。公投之为公投，固然是有一套非常严格的规定，以决定成功与否。而最大的分别，公投是对事不对人，补选则是拣出候选人。公社联盟以他们那5位辞职参与补选的候选人，作为支持自己设定的议题，那谁又可以代表议题的另一面。<br />
<br />
宪政权力是属于中央政府的权力，所以人大常委在07年底作出了有关的决定，而特区政府最近提交的2012年行政长官和立法会选举办法，也是以此为基础。公社联盟要挑战人大常委的决定，他们尽可以挑战，但谁又有资格可以代表北京中央，人大常委又或者特区政府去应战呢。<br />
<br />
公社联盟自己定义选举的性质，自己定义了公投议题，自己定义了成败条件，他们甚至一手包办谁是对手，总之不是他们公社联盟辞职那五个人，就是建制派。就算是建制派那几个人，但他们凭什么以个人的成败，去代表中央政府的宪政权力。张三李四、赵五黄六，自己出来选一选，就可以代表中央政府的宪政权力，这是什么法理、什么逻辑？<br />
<br />
补选代公投，是公社联盟搞出来的玩意，他们那部分怎么搞，是他们的事，但他们的政治对手不可能也出来玉成其事，把这违法违宪的行为合理化，在陷阱之上加一把禾秆草，让其足以乱真，更以一己之得失成败，把中央的宪政权力也拖落水！<br />
<br />
建制派最佳策略是置之不理<br />
<br />
有意参选的人，当然不敢承认自己是代表特区政府和北京中央而战，但当设定整个游戏的公社联盟也是白纸黑字公然以公投来定性，你又如何扭转全港700万人的感受观感。就算香港人明白，国际舆论媒体也可大做文章，到时又由谁去解释澄清。就算有理有据，他们又会否如实刊登。明知是贼船仍然照上，但又无法攻入驾驶舱，这个政治代价，又是否那些参选人士可以负得起。如果负不起，那又是由谁出来「埋单」。你自辩是补选，但设局者明白表示是公投，你叫公众信谁？<br />
<br />
对于这个公投把戏，建制派最佳的策略是置之不理，这不是示人以弱，而是不授人以柄，予以有可来之机，去抬轿做一件违法违宪的事。上届立法会选举投票率只有四成半，其中四成为建制派的票数。建制不选，泛民全部字数也只有二成七，民主党不参加，再加上没有两阵对员的竞争性，那两成七的基本盘，投票的随时一半也没有，拿着那一成多的投票率，公社联盟如何交代，又如何言胜？<br />
<br />
