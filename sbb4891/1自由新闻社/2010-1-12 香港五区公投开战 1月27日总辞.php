													<h2>陈成斌s变相公投的世界先例</h2>
						
						
																											<div id="postmessage_448628" class="t_msgfont">明报网上版 2010.1.23<br />
--------------------------------------------------------------------------------<br />
陈成斌s变相公投的世界先例 <br />
<br />
【明报专讯】当公民党和社民连合作的「5区请辞、变相公投」（或简称5区公投）成事后，坊间很多评论都认为这样的公投达不到世界各地的标准，有些则认为这只是一场补选而非公投，而政府的一贯响应则是香港没有公投的法律基础和效力。这样的评论或许有他们的道理，但在政治上却不一定是事实，特别是以议员席位的改选来做成变相公投，世界各地其实并非没有先例。<br />
<br />
就以日本为例，2005年时，当时的首相小泉纯一郎推动的邮政民营化改革法案无法在参议院通过。根据日本宪法，要推翻参议院的决议的话，小泉需要众议院三分之二以上的议员再度通过才行，但当时小泉没有得到足够的众议员支持。于是，小泉毅然运用他作为首相的权力解散众议院重新选举，以此作为变相公投来推动他的邮政改革。尽管当时有些评论不看好这种变相公投，但最终支持邮政改革的候选人在改选中获胜，达到小泉所需的三分之二多数。<br />
<br />
变相公投关键在泛民否决权<br />
<br />
香港有没有可能出现这样日本小泉式的变相公投呢？答案是肯定的。《基本法》第50条订明，当立法会拒绝通过政府提出的重要法案后，行政长官可以解散立法会重新选举。当然，这样做有其限制和后果。限制是行政长官一任任期内只可解散立法会一次，而后果则是若重选后的立法会仍然拒绝通过所争议的原案，根据《基本法》第52条（3）项，行政长官必须辞职。以此来与日本对比，香港政府应在泛民否决政改方案后，由特首宣布解散立法会重新选举，以便取得三分之二多数，也就是打破泛民对政改的否决权，一如小泉当年要取得众议院三分之二多数来推行他的邮政改革一样。<br />
<br />
尽管各国的公投办法有所不同，但不论是如西方国家那样的直接公投，又或者是日本小泉式的变相公投，一致的是他们都以公投解决严重政治纷争，因为这种做法对社会的成本其实反而是最低的。可惜的是，尽管特首有主动解散立法会的权力，但政府似乎不会以此解决当前的政治僵局。小泉能够义无反顾地诉诸民众来得到他所需要的三分二多数，曾荫权和港府却不敢如此做，香港的困局由此而来。<br />
<br />
一般而言，非执政党派是较难主动发动公投的，但因为香港采用地区比例代表制，于是泛民便能借香港这种特殊的政治制度来作5区请辞，以此来达至公投效果。这种公投不仅是直接公投的变相，更是日本小泉式公投的变相。尽管社民连和公民党提出的公投题目是「落实真普选，取消功能组别」，真正骨子里要公投的其实是泛民对政改方案的否决权。如果民众同意政改方案的话，便应该让泛民输掉三席以上，令泛民失去政改否决权。反之，若泛民还能够在新的民意中继续维持其否决权的话，则可见政改方案不得人心，道义上需要辞职的反而应该是特首本人。至少，政府应该撤回原有方案，提出更合乎民主的政改方案来。<br />
<br />
就此来看，公民党和社民连的5区公投其实不单不是在浪费公帑，反而是替政府省下了解散立法会全面重选的开支。香港市民一直未试过有如此接近公投的机会，也未试过可以在这个残缺的政治制度里将民意完全表达出来。这次公民党和社民连愿意拿上泛民否决权来给市民选择，为香港政制打开缺口，是值得我们称赞的。<br />
