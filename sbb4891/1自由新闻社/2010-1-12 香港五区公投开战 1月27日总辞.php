20100123 21:25:36&nbsp; &nbsp;&nbsp; &nbsp;<br />
 <br />
（中央社记者张谦香港23日电）香港民主派中的公民党和社会民主联机为争取普选而提出的请辞方案，遭到较亲政府的自由党杯葛。<br />
