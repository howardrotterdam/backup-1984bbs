黄毓民<br />
<br />
五名立法会议员下星期会辞职，正式启动五区公投。李家皓摄<br />
<br />
澄清绝对不是搞港独<br />
<br />
五位即将结束议会生涯的议员，昨与五区公投运动总发言人、公民党党魁余若薇一同会见记者。 1991年已晋身议会的陈伟业形容，是次补选将是历来最有意义的选举，因为市民可以直接就「尽快实现真普选、废除功能组别」议题表态。 04年才加入立法会的梁家杰强调诉诸群众手上一票，是目前解决不健全政治制度的唯一办法。同样在 04年踏入议会的梁国雄庆幸能在特区民主关键时刻「做番少少S」，呼吁市民在补选中「投畀自己良知」。<br />
<br />
为免被计算入选举经费，辞职后再补选的名单未能正式公布，但据了解以上五位议员将原班人马在所属地区出选。余若薇强调：「运动唔系属于两个党，而系属于全香港市民」，希望支持民主的市民「全民皆兵」，共同为特区民主奋战；重申在这选议题而非选人的变相公投运动中，最重要是市民清楚了解公投议题，而非由谁出战，有信心透过连串落区活动后，支持运动者与日俱增。<br />
<br />
五区公投运动正式启动前，港澳办和中联办已开腔打压，批评公投违反《基本法》，亲北京阵营人士亦随即开炮狂轰五区公投旨在搞港独。余若薇昨严词澄清「绝对唔系」，强调《基本法》清楚列明香港是中国不可分裂的一部份，两党无意挑战，解释以「五区公投、全民起义」为口号，只是呼吁市民踊跃投票，反驳外界对搞港独的质疑是「上纲上线」及「辞穷理屈」，意在向他们「乱扣帽子」。<br />
<br />
余若薇又承认，若五名候选人中有三、四人在补选中落败，原本拥有 23个议席的泛民主派将失去政改方案否决权，若情况如此，她会尊重选民的选择，但这不代表民主运动终结。<br />
<br />
辞职是尽忠职守表现<br />
<br />
对于有亲北京人士指议员辞职后再参选，违反议员就任宣誓约章，或就此提出司法复核，余若薇称早已翻阅《立法会条例》，当中清楚列明议员可以辞职，亦提及候选人资格，强调五位即将辞职的议员参选时均以力争双普选为参选政纲，「今次辞职亦都系争取普选，完全无任何违反。」黄毓民也称他们为落实竞选承诺而以辞职争取尽快普选，明显是「尽忠职守」的表现。<br />
<br />
除了中央高调打压，泛民主派亦有被抹黑危机，有消息指有人已掌握黄毓民的黑材料。黄毓民昨称「出得行预要还」，直言「预要淌政治浑水，就会俾污水泼到」，强调不担心任何打压。<br />
<br />
立法会主席曾钰成昨在公开场合响应称，就算有部份议员辞职，也不会影响立法会工作，深信政府要立法或申请任何公共开支，都会按既定程序进行，不存在趁泛民多名议员辞职而出现「乘机博乱」情况。<br />
