													<h2>苹论：「五区公投」是中央逼出来的！</h2>
						
						
																											<div id="postmessage_443606" class="t_msgfont">Apple Daily 苹果日报网上版－－2010-01-21<br />
------------------------------------------------------------------------------<br />
苹论：「五区公投」是中央逼出来的！<br />
<br />
公民党、社民连连手发动的「五区总辞变相公投」行动还未正式展开，请辞的议员还未辞职，北京中央政府就已急不及待出手打压。前几天国务院港澳办公室发言人、中联办就发表强硬声明，批评「五区公投运动」是公然挑战《基本法》和人大常委会有关政改的决定，「不利于香港政治体制在《基本法》框架内循序渐进向前发展，不利于香港社会政治的稳定和经济的发展，有损于广大香港市民的福祉」。<br />
<br />
北京中央政府的批评不可算不严厉，不可算不激烈，跟当年批判港督彭定康的政改方案为「三违反方案」差不多。问题是这些批评都是意气之词，既没有根据，也欠缺说服力。首先，根本不存在「五区公投运动」挑战《基本法》或人大常委会的问题。所谓五区公投运动顾名思义是一场政治运动，是一场群众运动；它不是严格意义上的公投，更不是具有宪法意义上的全民投票。明白这个性质就可以知道公投运动跟《基本法》这部宪制性文件没有甚么关系，跟人大常委会的独特宪制权力及位置没有甚么矛盾。既然是这样，北大人凭甚么说「五区公投运动」挑战《基本法》或人大常委会呢？<br />
<br />
此外，「五区公投运动」的议题、目标是争取尽快落实真普选及取消功能组别议席。根据《基本法》第四十五条及六十八条，行政长官、立法会全部议席最终都要由普选产生。任何不怀疑当年《基本法》起草委员会及人大常委会诚信的人都会相信，他们起草及通过的四十五条、六十八条所指的是真正的普选，是跟国际所理解的普选意思一致的真普选。而只要《基本法》规定的是真普选，立法会的功能组别自然要寿终正寝，送进历史档案中。现在，五区公投运动的主旨是要落实真普选及废除功能组别议席，这跟《基本法》的目标是一致的，根本不存在所谓损害政改进程的问题。<br />
<br />
至于说五区公投运动损害社会稳定、经济发展及市民福祉同样是毫无根据的指控。从具体操作来看，五区公投运动是一次全港性补选，是一场透过选战动员市民就政改表态的行动。从过往几十年的政治发展史、选举历史来看，这类助选、选举活动从来没有引起甚么暴力冲突，从来没有引发任何动乱、暴乱。即使选情最激烈的时候，参选人、助选团、政党都能恪守选举规则及香港其它法律，未出现过影响社会秩序或稳定的情况。今次五区公投运动当然会有大量的助选、宣传活动，当然会有一场又一场的激烈辩论；但谁也看不到运动跟过往的选举或群众动员有何本质上的分别、也看不到有甚么暴力的种子或势头。在这样的情况下，怎么能说「五区公投运动」不利社会稳定或损害市民福祉呢？<br />
<br />
其实，两个民主派政党及其它团体之所以推动「五区公投运动」这种另类的政治运动完全因为政改始终徘徊不前，完全是因为中央政府、特区政府在全面民主化发展上总是顾左右而言他，让市民一次又一次失望而回。要是北京中央政府及特区政府能让市民清楚看到迈向全面普选的路线图，让市民知道香港正踏实的一步一步走向真普选；像「五区公投运动」这样的运动根本不会出现！<br />
