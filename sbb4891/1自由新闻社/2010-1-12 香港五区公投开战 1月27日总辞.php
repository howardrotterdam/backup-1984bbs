													<h2>不畏极权追杀 公投五侠上刀山</h2>
						
						
																											<div id="postmessage_445674" class="t_msgfont">Apple Daily 苹果日报网上版－－2010-01-22<br />
--------------------------------------------------------------------------------<br />
不畏极权追杀<br />
公投五侠上刀山<br />
&nbsp;&nbsp;<br />
【本报讯】五位来自公民党及社民连的立法会议员将在下周三宣布辞职，暂时告别议会生涯，转而在「五区公投运动」披甲上阵。面对中央出手打压、亲北京阵营连环抹黑，以及随时丢失议席落败而回，踏上刀山火海之路的公投五员呼吁港人「全民起义」，在透过补选进行的变相公投中，以选票对「尽快实现真普选、废除功能组别」表态，向强权说不。记者：许伟贤、莫剑弦<br />
<br />
即将请辞的五位议员包括港岛区公民党陈淑庄、九龙东公民党梁家杰、九龙西社民连黄毓民、新界东社民连梁国雄及新界西社民连陈伟业。五人下周二向立法会秘书处递交辞职信，翌日在立法会大会发表辞职演词，当晚 7时半在中环遮打花园参加由五区公投委员会发起的千人造势「五区公投运动起动大会」。辞职下周五正式生效。<br />
<br />
