<br />
　　李文在谈话中，重点提到了中国，他说，中国政府为了提升国内知名大学，如清华和北京大学的地位，使它们成为世界一流大学，近年来对高等学府投入了数千亿人民币，数额至少达到国内生产总值的1.5%。<br />
<br />
　　他说：“只需一代的时间，也就是25年内，中国的顶尖大学将可以和美国常春藤联盟大学分庭抗礼。”<br />
<br />
　　李文这次到英国，是受邀在伦敦皇家学会就亚洲大学崛起的课题发表讲话。</div>

							
							
							
							
