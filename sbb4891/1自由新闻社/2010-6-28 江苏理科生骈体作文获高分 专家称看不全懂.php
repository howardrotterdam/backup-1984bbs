																											<div id="postmessage_708482" class="t_msgfont">江苏理科生骈体作文获高分 专家赞其可读硕士<br />
<br />
2010年06月28日07:32<br />
<br />
<img src="http://www.jyb.cn/gk/gksx/201006/W020100628324599322996.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　“呱呱小儿,但饮牛,至于弱冠,不明犍状。罩豚,日食其j,洎其成立,未识]j。”在今年江苏高考以《绿色生活》命题的作文中,一名考生就以这样通篇骈体古文的方式完成,阅卷老师在读到此文时,视作“怀疑卷”提交给专家组。南京市语文基础知识阅卷组组长、古典文献学专家吴新江在看完全篇后,仅对文章的注释就写了4页纸,当地媒体急寻这名“古文奇才”,昨天这位学子终于露面。<br />
<br />
　　“不仅普通阅卷老师不认识、看不懂,就连我这个教古典文献的老师,也不全懂,有些见过但没用过,还有四五十个古字根本不认识。回去后我查阅资料,做了注释,光注释就写了满满4页!”阅卷老师、古文专家吴新江在评价这名学生时,自觉惭愧。他表示,事实上光会用文言,会写古字还不算什么,这毕竟是高考作文,切题才是第一要务。而这篇文章,熟练运用“恰到好处”的古文,紧扣“绿色生活”的主题,“古为今用,十分切题。”此外,今年江苏高考作文阅卷组组长何永康也对这名考生高度赞扬,他表示,“以他现在的文化修养完全可以被破格录取!”<br />
<br />
　　昨天,该生首度露面,是江苏如皋中学高三（12）班的学生,名为王云飞,家住如皋下原镇一个叫文庄村的地方,其父母都是农民。而且,他还是一名理科生,从他的高考成绩看,其总分已经超过了一本分数线。王云飞表示,自己平时就对古文比较感兴趣,先后自购了《史记》和《尚书》,曾经把《资治通鉴》翻烂,此前在多次考试中还尝试写过武侠小说、文言文小故事等。<br />
<br />
　　“高考前一天晚上,我还跟妈妈说,我要冒险了,我要写文言文。”王云飞说,“我觉得写得还不错,但心里也没底。不知道阅卷老师看了怎么想。”<br />
<br />
　　一位阅卷老师表示,“这位考生此方面造诣相当深,不仅会用古字,而且用得很得体,能在考场上短时间里活用古字,说明他已纯熟于心。此外,这篇作文也很有思想,他从历史发展角度来说明现在环境遭破坏,幻想能回到农耕时代,写得相当不错。专家一致认同给他打了高分。”这是江苏高考继2001年《赤兔之死》后又一篇文言文高分作文。一位专家表示,“《赤兔之死》虽然是用文言文写作,但毕竟是'三国体’,而这位考生则是纯正文言文,修养的确很深,直接读古文献的硕士都没问题。在网上,此文已经被多家媒体及网友追捧,有网友笑称作者为“穿越人”。<br />
