													<h2>为什么包括中国字在内的中国文化是劣等的？</h2>
						
						
																											<div id="postmessage_517484" class="t_msgfont">关于中国文化的一些杂感――与吕柏林先生玩拆字对句游戏之余有感<br />
<br />
&nbsp; &nbsp;如果我们站在一个全新的高度，完全摆脱掉身为中国人难免的那种对于“中国文化”的偏爱情绪，我们仔细分析“中国文化”的方方面面，我发现，不仅其核心精神，绝大部分的主要内容，只是糟粕，封建专制时代遗留下来的糟粕，而且即使是过去我认为是无害的，具有娱乐性的那极少部分内容，比如说文学艺术，也基本上是垃圾。 <br />
<br />
&nbsp; &nbsp;比如说中国古诗词，即使是对“中国文化”持完全否定态度的人，也难免会为之迷惑，我过去也曾一度很沉迷于此，不仅背诵，没事还将其反复抄写，抄了满满的几个笔记本，乐此不疲。可是最终我明白了，那些东西，不仅其思想内容贫乏阴暗得很，而且其艺术价值，从审美的角度看，也是原始低劣得很。 <br />
<br />
&nbsp; &nbsp;中国古诗，由于其格律要求的极端严格，为了一丝不苟地将字数凑得工整，而宁可牺牲掉对于对于思想情感的自由表达，最终只是沦落为一种弱智的拼字游戏，靠字与字之间无逻辑无语法规律的胡乱拼凑堆砌出一副字体图像，以此给予人视觉上一点点可怜的光怪陆离的刺激，靠所谓迂腐的韵律让人念着顺口而已。 <br />
<br />
&nbsp; &nbsp;而我们以中国古代最富盛名，达到了中国诗歌成就的顶峰的诗人――李白为例来说明，就更容易理解这个问题了。他之所以能取得最大的成功，恰恰在于其勇于摆脱中国诗歌的格律限制，在很多需要严格遵守格律要求的地方，他不拘一格，不受字数以及韵律工整的限制，随心所欲想自己所想，书自己欲书，清新流畅地表达出自己的思想情感，才使得他的诗歌具有了较高的艺术性，可读性，和长久的艺术魅力，这在之前以及之后都是少有的。这就正说明了中国诗歌缺陷所在。 <br />
<br />
&nbsp; &nbsp;但即使是这样，他的绝大部分诗歌也是没有多少思想价值和艺术价值的，前人就曾经批评他的诗歌是：十句九句酒与女人。 <br />
<br />
&nbsp; &nbsp;确实，剥开对中国诗歌和诗人种种滥美之词所做的华丽包装，什么歌以咏志也好，抒发抱负也好，壮志难酬也好，人生失意也好，说白了，中国古诗的主要内容，就是发牢骚，抱怨自己没有官做，没有钱花，没有酒喝，没有女人玩，或者得不到主人赏识，的种种牢骚，散发着一股腐臭的太监味道，完全没有一丝一毫的追求精神自由，追求人格独立和尊严的激情。 <br />
<br />
&nbsp; &nbsp;我们看到，西方文学，诗歌，从荷马时代开始，其中就充满着对人类命运深沉的慨叹，对自由的渴求，对反抗专制强权的追求。其思想性，艺术性的深刻，博大精深，完全不是粗陋乏味的中国诗歌所能够相比的。 <br />
<br />
&nbsp; &nbsp;虽然，李白也曾经假模假样酸溜溜地说什么“安能催眉折腰事权贵”，但是只要是稍微熟悉历史的人都知道，这只是他跟其他奴才在皇帝面前争宠失势后的牢骚话而已，实际上，他顶多不过是不愿“催眉折腰事权贵”，但是他却是很乐意很盼望“催眉折腰事皇帝”的，而且，真的一旦皇帝或者其它什么“权贵”召见他时，他跑的比谁都快，“仰天大笑出门去，我辈岂是蓬蒿人”。得意忘形得真的以为自己一有皇帝宠幸就高人一等了，不是俗人了，骨子里对劳动人民极为鄙视，可笑的是，皇帝却一直不过拿他当个娱乐用的弄臣而已，玩腻了就一脚踢开，“赐金还乡”，然后他就来满腹牢骚怨恨地在诗歌中发泄一通。 <br />
<br />
&nbsp; &nbsp;屈原，自命高洁，“举世皆浊我独清”，其实不过也就是在君王面前争宠失败被贬，怀着满心娘娘腔的幽怨，哀怨君王为什么听信其他奴才的谗言不再宠幸他，动不动就以没有男人抚慰的芳草美人自比，十足的怨妇心态。 <br />
<br />
&nbsp; &nbsp;陶渊明，看似很有追求身心自由的决心，实际上不过是为逃避乱世兵祸，东躲西藏寻找一个老鼠洞苟且偷生的消极避世。 <br />
<br />
&nbsp; &nbsp;杜甫，看上去好像比较关心民间疾苦一点，对社会黑暗批判得多一点，但是也不过就是不触及实质无所思想突破，不痛不痒矫情地虚表同情一番，不过也就是期待明君不遇之余的忧闷难遣而已。 <br />
<br />
&nbsp; &nbsp;中国诗歌的这种缺陷，可能跟中国文字的特点有重要的关系。我们知道，汉字是一种从象形文字过渡而来的表意文字，一种单音节文字，“每个单字通常都具有其独立的意义，成为单音节词，这种以数量有限的单音节词为主的简略的文字（思维载体）所能够表达的思想和所能承载的信息量都是极其有限的。 <br />
<br />
&nbsp; &nbsp;到了近代，面对从西方社会如潮水般涌来的新概念，新思想，东方人（包括日本人和中国人）在自己的文字中找不到相应的表达方式，不得不采取生搬硬套的办法，用汉字那种有限的单音节词组合成新的多音节词来表示这些新概念。 <br />
<br />
&nbsp; &nbsp;但是由于组成新词的每个单字本身都具有独立而且多元的意义，这就很容易使人对由单字构成的新词望文生义，产生与原来含义不同而且多元的理解，造成思维混乱。 <br />
<br />
&nbsp; &nbsp;虽然英文的词汇大多数也是由具有一定含义的词根和前后缀组合而成，但是其词根本身并不能作为词汇单独表达含义，而是要与其他成分组合在一起才有意义，这就使得英文词汇的意思得以严格定义，含义表达清晰而准确，对概念的分类细致而且界限明白。 <br />
<br />
&nbsp; &nbsp;而且因为这个特点，英文的词汇量得以扩展得很巨大，而每个词汇的意义又准确明白，定义严格。使得英文总体上能够承载的信息量很大，各个概念间又界限分明，不易混淆。 <br />
<br />
&nbsp; &nbsp;而汉语呢，只能跟在英文的发展后面，人家有了什么新概念，新词汇，自己才能生搬硬套地用原本就有歧义的单字生造出相对应的新词出来，不仅含义表达不清楚，而且其简略化的缺点仍然无法改变。”――拙作《四大发明的鬼话和汉字表达之关系》 <br />
<br />
&nbsp; &nbsp;汉字的这种简略，字义模糊的特点，再加上其从象形文字演变而来所遗留下的图像性，其单字从字形上也可以造成人与无穷的联想，这样，就更加大了其模糊性。 <br />
<br />
&nbsp; &nbsp;虽然很多热爱汉字的人很是把这一点看作是文学上的一种优点，赞不绝口，但实际上要成为一种流畅明晰的思维和语言载体，这可能是不利的。<br />
<br />
&nbsp; &nbsp;这种思维载体的弱智和低能，其能够承载的信息量的贫乏有限，其表达概念的模糊性，不精确性，简略性，可随意组合导致的多歧义等等原因，使得人的思维往往容易僵固，凝滞，偏执，而又时不时表现出无逻辑性的跳跃，散乱。<br />
<br />
&nbsp; &nbsp;这一点，热血汉奸论坛里喜欢沉迷于玩拆字和对句游戏的吕柏林先生就是一个很典型的例子。<br />
<br />
&nbsp; &nbsp;<a href="http://www.rxhj.net/phpBB2/viewtopic.php?t=10188" target="_blank">http://www.rxhj.net/phpBB2/viewtopic.php?t=10188</a> （请看二楼）<br />
<br />
&nbsp; &nbsp;这大概就是所谓的“汉字思维”吧。 <br />
<br />
&nbsp; &nbsp;我自己也是深受这种“汉字思维”的毒害。我以前也是跟很多人一样很沉迷于文言文，导致我以前的思维也是经常“僵固，凝滞，偏执，而又时不时表现出无逻辑性的跳跃，散乱”，一直到现在都还没有完全摆脱掉这种“汉字思维”的影响。<br />
<br />
&nbsp; &nbsp;好在后来我终于通过接触西方文化，学到了一点西方文化的思维方式，认识到了汉字，汉字思维，以及整个中国文化的有害。<br />
<br />
&nbsp; &nbsp;不知道大家注意到没有？用英文等西文撰写的文章，书籍（当然，主要是指西方人自己的著作，中国人还远远达不到那个水平），其对概念的解释，对事物的描写，对思想和理论的表达，诠释，其逻辑清晰程度，严密程度，深刻程度，其接近事实真实的程度，流畅明白的程度，是用中文写作的中国人的作品远远望尘莫及的。 <br />
<br />
&nbsp; &nbsp;我们就没有必要用文言文这种思维简单概念模糊信息量贫乏简略的原始文字来对比了吧，免得更没面子。就用语法和词汇都很大程度上西化了的现代白话文来对比。即使是这样，中文的表达力都是远远不如英文的。 <br />
<br />
&nbsp; &nbsp;即使是从英文（或其他西文）翻译而来的文章或书籍，虽然其原意在翻译过程中被中文严重曲解了很多，但是其逻辑清晰严密，思维的深刻，概念的清楚，也仍然是靠“汉字思维”直接用中文思维书写的中国人的作品所远远不及的。 <br />
<br />
&nbsp; &nbsp;我很早的时候，第一次看到这些西方原著时，就感到很震惊，我没想到，同样一样事物，用别人的话说出来，就是那么清楚明白，很多多年的疑惑一下子就豁然而解。 <br />
<br />
&nbsp; &nbsp;这说明了：使用两种不同文字和语言并以其为思维载体的不同人群，其思维方式和能力存在着巨大的差距。 <br />
<br />
&nbsp; &nbsp;很多人可能都知道，要把什么中国古诗之类的翻译成英文等西文是一件很困难的事情，但是我们知道这根本不是因为什么中文的所谓复杂高深，因为使用英文等西文思维能够产生出欧氏几何学，微积分，牛顿力学，相对论，量子力学等西方科学，能够发明出蒸气机，火车，汽车，飞机，电脑，互联网，原子弹。不可能几乎什么科学和文明成就都没有创造出来的中文思维还比英文思维更高明。 <br />
<br />
&nbsp; &nbsp;仔细想想就很容易明白了，因为文言文，根本就是一种故意装神弄鬼的文字，它的功能和使用目的，就不是为了要能够清楚明白地表达事物，和思想。相反其目的就是为了要故意把话说得让人看不明白，故意装神弄鬼，以掩饰自己的思维肤浅，贫乏。 <br />
<br />
&nbsp; &nbsp;而从这种弱智文字演变而来并还残留着极大后遗症状的现代中文，也存在着同样的问题。<br />
<br />
&nbsp; &nbsp;而英文等西文恰恰相反，其目的就是要尽可能地将话说得明白，尽可能地将事物表达解释得清楚明白。 <br />
<br />
&nbsp; &nbsp;所以把中文古诗或者其他文言文翻译成英文，就好比是要用一个思维正常的人的语言把一个疯子的呓语或者一个婴儿无意义的丫丫儿语解释清楚一样的道理。 <br />
<br />
&nbsp; &nbsp;而要把概念清晰，逻辑严密，语法复杂，表达流畅，所包含信息量巨大的英文著作用表达能力很差信息量很贫乏的中文完全不失其本意地翻译出来，也同样是一件很困难的事情。这就好比是要让一个疯子或者一个婴儿能够理解思维正常的人的语言和思想一样的道理。 <br />
<br />
&nbsp; &nbsp;因为这个原因，所以近代以来很多从西方翻译介绍而来的哲学，文学，科学著作，其原意都被严重地歪曲，被中国人错误理解，并导致中国人在社会实践上错误地走了很多弯路。 <br />
<br />
&nbsp; &nbsp;比如说，连中国人自己都承认：自称信奉马氏共产主义的中国共产党（其实应该叫中国伪共产党），其实他们从来都没有人真正弄明白了马克思主义是怎么回事。结果在瞎折腾了几十年之后，在饿死了几千万人之后，在国民经济濒临崩溃，付出了巨大的代价以后，他们仍然只好又从头向西方学起，从头开始搞血腥的资本主义原始积累。 <br />
<br />
&nbsp; &nbsp;不仅这样，中文的模糊性还导致另一个很大的恶果：那就是中国人在辩论时，往往喜欢并且也很容易有意无意地歪曲别人的原文意思，然后用被自己歪曲过的话偷换概念去攻击别人。所以中国人之间的辩论到最后往往不是达到“求同存异”“真理越辩越明”的良性目的，而是发展为恶意的人身攻击，或者自说自话，或者胡搅蛮缠。 <br />
<br />
&nbsp; &nbsp;从文言文这种弱智思维载体产生不出逻辑清晰严密，认识深刻的西方式哲学，当然也就更加产生不出现代科学，虽然现代中文经过修正，能够勉强接受和理解西方现代科学知识，但是对于真正的科学精神仍然是没有能够完全理解的，这一点，相信大家应该都无法否认。 <br />
<br />
因为很明显的，科学比哲学还更加需要概念清晰，逻辑严密，推理计算一丝不苟，结论滴水不漏，这个可是不能开玩笑的，要不然飞机卫星火箭上了天也会掉下来。哲学还可以百家争鸣一下，各个流派各有各的见解，而科学，对于一种事物，经过去伪存真之后，一般就只能有一种确定的，经得起实验检验的理论来解释它，最多不过需要以后再修正一下。 <br />
&nbsp; &nbsp;试想，各门科学中，每一个概念的定义，每一个定理，定律，每一种理论的诠释，特别是每一个公式，每一个参数，这些，没有意义清晰，界定严格到极致的程度，怎么能够再进行下一步的逻辑推论，归纳，演绎，计算出结果，推导出过程。 <br />
<br />
&nbsp; &nbsp;而以文言文这种极度的思维简单弱智、概念模糊、信息量贫乏、简略的原始文字为思维载体，产生不出现代科学，当然也就好象是鸡飞不上太空一样简单明了的事实，这还有什么可争辩的吗？ <br />
<br />
&nbsp; &nbsp;所以中国人还在傻乎乎地拿着李约瑟这个国际骗子的“李约瑟伪问题”苦思冥想“为什么中国古代没有产生出现代科学”并意淫“中国古代本来是可以产生出现代科学的”不是一件荒唐可笑到极点的事情吗？这本身可能就是“汉字思维”的一种病态表现吧？ <br />
<br />
&nbsp; &nbsp;而汉字思维的这种僵滞，偏执，简略，不流畅，是不是也是造成中国人音乐细胞不发达的一种重要原因呢？（毫无疑问的，中国从古到今的音乐成果都是远远不能与西方文明相比的，简直是天壤之差，这一点相信无人可以否认），或许也值得研究一下。</div>

							
							
							
							
