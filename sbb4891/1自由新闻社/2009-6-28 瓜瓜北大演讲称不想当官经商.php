<br />
在网上看到近来新闻不断的薄瓜瓜将做客北大英杰交流中心，怀着一份好奇与朋友一起赶到了北大。今天来的人还真不少，想必大家与我一样，对这位有着特殊背景的“十大华人杰出青年”充满了好奇，听众中有很多是带着孩子来的家长，想必是告诉孩子，你要向瓜瓜学习，长大后也留学海外，为父母争脸。但他们无疑要失望了，无情的保安把我们这些没有学生证的人挡在了门外。<br />
<br />
不到一会儿，一群身穿白衬衣，头戴麦克的年轻人簇拥着一个身穿黑色西装的人从侧门进入了英杰中心，门外发出阵阵议论，很多人说穿黑色西装的就是薄瓜瓜。（其实他不过是一名便衣，真正的瓜瓜便在那一群白衣警卫中）<br />
<br />
7点钟刚刚过，保安例行公事似的放我们进了第一道门。但这时我们才发现，里面还有一道门。把守这一道门的除了几个保安，还有那些身穿白衣的警卫。有的人试图进入大厅，但被挡了出来，很多人质问，现场还有座位，为什么不让进？有很多人小声议论，不就是薄熙来的儿子吗？有什么了不起？小布什来也不至于这样啊！还有人说，前几天诺贝尔奖得主来北大，我们还进来听了呢。正在这时，双方发生了冲突，一名带着小孩的女人试图说服警卫，但被挡了出来，双方发生了争执，场面一度混乱。有同学感叹：真是场内瓜瓜叫，场外叫呱呱... ...<br />
<br />
我走出了英杰中心，才发现场外听众不断涌来，有意思的是，场内听众陆陆续续的有人出来，出来的人们在重复着一句话真没意思！<br />
<br />
我是后来才进入会场的，并没有听到瓜瓜全部的谈话。演讲台两边站了两排白色衬衣、头戴耳麦的警卫，会场里不时有警卫走来走去。警卫走过一同学身旁，那位同学小声说道“吓死我了”，脸上露出夸张的表情。让我颇感意外的是，不是说演讲吗？怎么是与主持人谈话聊天？或许，瓜瓜更喜欢这种交流方式，或许瓜瓜还不擅长演讲。身边的听众在小声的议论着，“不着调”，“语无伦次”，“胡言乱语”。我想我应尊重事实，我不能违心的说，对话有如何的精彩，瓜瓜是如何的风度翩翩，有才气，有涵养。与很多媒体以往的报道不同，瓜瓜在我眼中还只是个孩子，一个与我们同样普通的孩子。<br />
<br />
在谈到那些所谓的逛夜店的照片时，瓜瓜再次解释道，那是化妆舞会上照的，在英国经常举行化妆舞会。有同学问道，你是否是共产党员？是否信仰共产主义？将来是否回国？瓜瓜说自己是无党派人士。自己热爱自己的祖国，将来无论回到祖国，还是留在国外，都会为国家作出贡献。此时此刻，我想起了我们的外交部发言人秦刚。<br />
<br />
演讲结束，不，是座谈结束，我心中仍有很多疑惑，为什么瓜瓜会成为“十大华人杰出青年”？是龙生龙、凤生凤？是瓜瓜十分的努力，也相当的优秀？还是某些网民阴暗心理的猜测，人家有一个好的爷爷与好老爹。瓜瓜每年数十万的留学费用从何而来？媒体为什么要热炒瓜瓜？他为什么选择在这个时候来北大进行讲座？今天的那二十多位身穿白衬衣的小伙子是何身份？瓜瓜，你为什么要尿尿？尿尿也就罢了，还要拍照。未来瓜瓜的路在何方？<br />
<br />
不管怎样，我想今天所有来听讲座的所有北大的、非北大的老少爷们们心中有个数，他们对瓜瓜有了一份了解，心中有数。<br />
<br />
不知，明天的媒体又该如何报道这次讲座？我不知道。不想评论，只是记述我的一点所见所闻。<br />
