王冕画作《墨梅图》。（人民网记者文松辉翻拍）<br />
<br />
譬如“杨震却金碑”，讲述了“暮夜却金”的典故。杨震是东汉时期著名的儒臣，他出任东莱太守时，途径昌邑县，昌邑县令王密趁夜深无人，送十斤黄金给他，说夜深人静无人知晓。杨震说道：“天知，地知，你知，我知，何谓无知！”王密听后十分羞愧地离开。杨震因此被称为“四知先生”。“李白手迹《上阳台贴》”。1956年，毛泽东转交故宫博物院收藏的张伯驹赠送的李白手迹《上阳台贴》。毛泽东对李白的诗词颇为推崇，对这件可能是李白唯一存世的墨迹爱不释手，但他遵守党和国家领导人所收礼品一律上交国家的规定，将这件墨宝转送给故宫博物院。“朱德的扁担”，见证了朱德与战士们同甘共苦的情怀。井冈山斗争时期，朱德曾任红一方面军总司令、红军总司令、中华苏维埃军事委员会主席等职。虽然身居高位，朱德却经常和战士们一起挑粮上山。红军战士为了让他休息，两次拿走他的扁担。朱德便请军需处的同志为他削了第三根扁担，写上“朱德扁担，不准乱拿”八个大字。<br />
<br />
展览还特别展示了雷锋、焦裕禄、王进喜、孔繁森、郑培民、王瑛、沈浩等领导干部楷模和英雄模范人物的生动事迹。此外，展览还精选一些了揭露贪官污吏卑劣言行的文物。《金谷园图》，描绘的是西晋时期的巨贪石崇在金谷园中奢侈享乐的场景。《天水冰山录》，是明代贪官严嵩被抄家的清单。《和|犯罪全档案》，见证了和|的贪婪。<br />
