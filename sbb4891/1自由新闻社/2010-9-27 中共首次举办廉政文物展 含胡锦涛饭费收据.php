<br />
人民网9月27日报道&nbsp; &nbsp;胡锦涛于2002年考察西柏坡期间自付饭费的收据、朱德的扁担、方志敏的《清贫》手稿、李白唯一存世的手迹《上阳台贴》、西周“大盂鼎”等370多件反映中国廉政文化的珍贵文物27日将在国家博物馆开展，并向公众限额免费开放。<br />
<br />
文化部党组成员、中纪委驻文化部纪检组组长、展览组委会常务副主任李洪峰昨日在新闻发布会上介绍， 由中纪委、监察部、文化部、国家文物局联合主办的《浩然正气满乾坤――中国廉政文物精华展》将于9月27日至10月31日在国家博物馆展出，这是继2008年举办的“全国廉政文化大型绘画书法展”之后的又一次大型廉政文化展览，也是建党建国以来关于廉政文物方面的首次主题展览。<br />
<br />
由国家博物馆和上海博物馆承办的本次展览，在全国范围内征借了370多件文物，包括48件一级、珍贵文物，涉及古籍、书法、绘画、青铜器、瓷器、竹简、碑拓、生产工具、生活用品、书信手稿、珍贵照片、文献资料等不同类别。在展览的布置上，以时间为线索，将展览分为历史镜鉴和廉政风范上、下篇。上篇反映了历史先贤的精神境界和道德情操，下篇鲜明展示了中国共产党在领导中国革命、建设和改革的伟大历程中不断探索反腐倡廉建设的规律，不断丰富中国特色反腐倡廉道路的理论和实践内涵及清正廉洁、艰苦奋斗的优良作风。<br />
<br />
<br />
文物是历史的见证。在中华民族五千年悠久灿烂的文化中，以崇尚廉洁、鄙弃贪腐为核心的廉政文化始终占有着重要地位。此次参展的西周“大盂鼎”、“召伯甘棠图碑”、“杨震却金碑”、宋刻本《资治通鉴》、文天祥行书上宏斋贴卷、反映朱元璋重典治吏思想的《大诰》、王冕画作《墨梅图》、石涛的《爱莲图》、海瑞行书札、于成龙行书格言轴、林则徐行书、李大钊手书赠与杨子惠的“铁肩担道义，妙手著文章”对联、方志敏的《清贫》手稿、警卫员为周恩来补袜子用的袜板、刘少奇《论共产党员的修养》手稿、邓小平用过多年的牛皮腰带、陈云要求妻子于若木退还工资的收据、江泽民给西柏坡纪念馆的题词“牢记两个务必&nbsp;&nbsp;建设有中国特色的社会主义”、胡锦涛于2002年考察西柏坡期间自付饭费的收据、1956年毛泽东转交故宫博物院收藏的张伯驹赠送的李白手迹《上阳台贴》、朱德的扁担等370多件文物精品，每一件文物都蕴含着一个生动的廉政故事。<br />
