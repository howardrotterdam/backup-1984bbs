2010-09-27 06:00:30　来源: 人民网(北京)<br />
<br />
核心提示：9月27日，370多件反映中国廉政文化的文物将在国家博物馆开展，并向公众限额免费开放。参展文物包括：胡锦涛于2002年考察西柏坡期间自付饭费的收据、朱德的扁担等。此次展览由中纪委、监察部、文化部、国家文物局联合主办。<br />
