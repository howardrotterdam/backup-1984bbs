=========正文<br />
大纪元5月25日讯】(大纪元记者乔琪采访报导) 美国众议院议长佩洛西率代表团昨天抵达上海，展开对中国为期八天的访问。5月25日，北京两三千名访民在南站以示威游行的特殊方式欢迎佩洛西，呼吁她关注中国的人权，在同警方的冲突中十多人被抓。据称，这一次示威游行是自南站形立上访村以来最大的示威抗议活动，其规模、人数、口号、标语，时间都创造历史之最。<br />
<br />
据估计现场两三千名访民，有的喊着“打倒腐败”、“还我人权”、“民主万岁”，有的穿着“卖肾 为儿伸冤”的状衣、有的撒传单、有的手持着状纸，更有的喊“大家一条心”，“欢迎佩洛西，关注中国人权！SOS”，这醒目的横幅横在马路中间，持续二个小时。<br />
<br />
据山西访民李莉表示，25日原计划在早上9点半在南站附近欢迎美国众议院议长佩洛西到中国来访问，未料访民们抵现场时已有十来辆警车、大批警察守候。9点40分他们开始行动，不久有一访民被2位截访的人强行截走，访民们上前阻止，引发了冲突。<br />
<br />
上访的2年间3次服毒、2次割腕自杀未遂的河北赵春红也到了现场，据她表示与李素珍(音)当场拉起了“欢迎佩洛西访华 关注中国人权SOS”的横幅，并喊了口号，警察像土匪流氓一样将怀孕8个月的她拖倒在地，她的腿被拖伤，手被扭伤，另有几个老太太被打倒在地，也有好多戴着墨镜黑社会人物对访民出手，由于他们都是些老弱妇残，当时有位上海的年青人关军，将黑社会人打跑了，后来警察把关军与一位新疆的访民带走，他们担心警察会对他们下毒手，希望媒体关注。<br />
<br />
来自重庆的周光富表示，现场人很多，有2000多人，当赵春红拉起了横幅，警察过来抢，于是与访民发生拉扯推打，很多人受伤，也有70多岁的老人被黑社会的人推倒在地，激起民愤，他说警察甚至对赵春红说：“你以为我打不死你啊！”周同时表示，这是首都，我们欢迎外国访华团体，他们这样对待访民，实在太不应该。<br />
<br />
周透露今天警方抓了10多个人，包括一个外国媒体记者，后来据说接到上级指示，不能再抓人了，才把这些人都放了，但是把关军抓走，他说有7、8个警察对他殴打，他好像是有武功一样，一人独斗还占了上风，最后他被带走了。周表示：“我们都很惦记他，中国人很期待有这样的正义之士，为老百姓说话，我们呼吁中国人不迫害中国人，中国政府不迫害中国人、不杀害中国人。”<br />
<br />
现场访民有的喊着“打倒腐败”、“还我人权”、“民主万岁”，但都被警方制止，有的穿着“卖肾 为儿伸冤”的状衣、有的撒传单、有的手持着状纸，更有的喊“大家一条心”，很多人都表示“10年、20年不给解决问题，被逼的没办法。”<br />
<br />
珠海访民陈风强接受采访时表示，因为美国议长佩洛西曾多次公开批评中国人权，所以访民们自发性的组织这次欢迎佩洛西访华仪式，虽在大批警力的制止、监视下，及访民情绪的高涨下，总算完成了这个仪式，这次规模很大，以他的估计有二、三千人，很多的访民把这次的群体活动当作伸冤的场所。遗憾的是关军为伸张正义被押，关在北京右安门派出所。<br />
