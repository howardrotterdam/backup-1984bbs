																											<div id="postmessage_102729" class="t_msgfont">北京南站众人游行抗议 “打倒腐败、还我人权” <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;<br />
<br />
--------------------------------------------------------------------------------<br />
 <br />
[<a href="http://www.913hr.com" target="_blank">http://www.913hr.com</a>]&nbsp; &nbsp;&nbsp;&nbsp;摘自：澳门日报&nbsp; &nbsp;&nbsp;&nbsp;点击：&nbsp; &nbsp;&nbsp;&nbsp;发布时间：2009-5-26<br />
 <br />
 <br />
5月23日早上约600名来自中国各地的访民，自发到北京南站的最高人民法院信访口附近马路进行游行抗议，众人齐声高呼“打倒腐败、还我人权”，现场有的访民拿着自己的冤书、有的为邓玉娇声援、也有的穿着丧服，由于不断有访民到场声援，一度造成二环路交通中断。 <br />
<br />
<br />
据访民表示，今天(5月23日)早上9点30分左右，大约600名访民自发的到北京最高人民法院信访口附近的马路进行游行抗议，这些都是来自全国各地的老访民，到了10点钟左右现场包括围观群众已达1000多人。 <br />
<br />
<br />
<br />
现场的周先生表示，23日，在北京的访民自发的组织了一个壮观的抗议游行队伍，早上9点30分，访民们在最高人民法院信访口附近游行抗议，二环路现场交通中断，不久警察赶至现场疏导交通，并拉起了警戒线。有三个像黑社会的人突然出现在现场推打访民，后来被访民追打后逃逸，但警察否认是警方雇用的打手。 <br />
<br />
<br />
<br />
访民白女士称，现场有的拿着冤状、有的声援女杨佳邓玉娇、有的大喊：“不要再上当了，我们绝不回去、不离开北京”，更有的高呼“打倒腐败、还我人权”。 <br />
<br />
<br />
<br />
贵州访民陈桥员(音)带着4个3至10岁的小孩，孩子们穿着白色孝服孝帽拿着冤书示人。据陈表示，他的爱人于今年4月16日在云南的铅矿工厂工作，被掉下来的矿石砸死，但工厂不给任何赔偿，也不予理会，他们伸诉无门，悲惨遭遇引起了访民的同情，访民们纷纷解囊，也为现场紧张的气氛，增添了一幅感人的画面。 <br />
<br />
<br />
<br />
新疆的陈先生说：“政府光截访，不给解决问题，民怨沸腾，访民已压不住情绪了，这一次都发泄出来了，把高等法院门口的路都给堵了，但这是他们自发性的行动，希望得到政府的重视，其实也没有其他的办法了。” <br />
<br />
<br />
<br />
记者致电右安门派出所，据接电话的女姓职员表示，不接受国外媒体的采访。 <br />
<br />
<br />
<br />
云南李忠英表示，她因4年前手术致残，省立医院说她是病症太多致残，非手术所致，所以对她的申诉不予理会，政府以“生活困难”的理由，补偿了她3000元，她同时表示为了治病，她变卖了房子，导致倾家荡产，现在行动不便是个残疾人。上访多年，至今也没个合理解决她的问题。 <br />
<br />
<br />
<br />
重庆访民张静芬因住房遭强拆，上访多年，被劳教一年，最近才从劳教所出来，她说：“最不可理解的是以“流氓、卖淫”的理由劳教， 50岁的人了弄个卖淫的罪名，我上访除了讨回公道外，还要求恢复名誉。” <br />
<br />
<br />
<br />
访民们表示，今天(5月23日)来了三、四十个警察驱散访民、并疏导交通，至10点40分道路基本上已可通行，但很多访民不愿散去，有的拿着冤书，在道路二旁站着或坐着。由于天热，有个六十来岁的访民昏倒，警方却坐视不管，还是访民们打电话，才由救护车载走。</div>

							
							
							
							
