													<h2>清华公共管理学院教授强调绝不能搞“三权分立”</h2>
						
						
																											<div id="postmessage_607010" class="t_msgfont">凤凰网资讯 &gt; 大陆 &gt; 正文清华学者：“三权分立”西方也少用 中国绝不能搞2010年05月10日 07:38人民日报【大 中 小】 【打印】 共有评论19条<br />
清华大学公共管理学院 胡联合 胡鞍钢<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;中共中央宣传部理论局编著的《六个为什么――对几个重大问题的回答》（学习出版社出版），是一本正本清源的重要理论著作。该书强调一个国家的政治体制要适应国情的需要，强调我国不能搞“三权分立”，同时客观地指出“三权分立”在西方世界实际上只有极少数国家实行，而大多数国家并未实行“三权分立”。这对于澄清人们的模糊甚至是错误的思想认识具有重要作用。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;长期以来，一方面总有极少数人极力鼓吹“三权分立”的政治体制模式，妄图改变我国政治体制改革或司法体制改革的方向，甚至鼓吹改变我国的根本政治制度；另一方面也有不少人认为西方国家甚至外国都是实行“三权分立”的，似乎不搞“三权分立”就理不直气不壮，有的甚至认为“三权分立”是所谓“国际惯例”。那么，实际情况到底如何呢？事实上，在西方国家中，只有美国（总统制）实行“三权分立”，而其他绝大多数西方国家实行的是议会制，并未实行“三权分立”。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;一个国家实行什么样的政治体制，不但与其发展的历史、国情密切相关，而且与其阶级特性密切相关。西方国家的政治体制是国家的上层建筑，是维护资产阶级利益的机器。即使同是西方资产阶级国家，其政治体制的形式也不尽相同。西方国家的基本政治体制形式可以分为两种，一种是总统制，一种是议会制。这两种政治体制最关键的区别在于行政首脑产生的方式不同。在议会制下，选民选出议会，议会推选出总理（首相），总理（首相）挑选内阁成员而形成政府（行政当局）。在总统制下，选民一方面要选议会，一方面要选总统，然后由总统来挑选他的内阁成员。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;西方议会制国家的显著特点是，立法权与行政权不分立。议会不但是国家的立法机关，而且是国家的权力中心。行使行政权的那些人来自议会（下院），包括总理也是由议会推举的（一般是议会多数党的领袖）。他们既属于立法部门，又属于行政部门。也就是说，立法权与行政权在实质上是合一的。两种权力不但共生，即产生议会成员的选举也间接是产生总理（首相）的选举；而且共灭，即总理（首相）必须保持议会大多数成员的支持，否则要么下台，要么解散议会而重新举行大选。不但如此，一般而言，议会制是没有任期限制的，只要获得议会多数的支持，总理（首相）就可以永远干下去。在议会制下，政府（内阁）由议会中占多数席位的政党（或执政联盟）组成，并对议会负责。英国是典型的议会制国家。在议会制下，政党政治实质上是主宰议会政治的幕后之手，“议会至上”实质是“执政党至上”。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;西方总统制国家的显著特点是，总统掌握最高行政权，行政权、立法权、司法权是分立的。在总统制下，总统是行政首长，直接任命并领导内阁，但内阁不像议会制下有决策权，而是总统的咨询机构；总统不向国会负责，也无权解散国会。另一方面，国会也不能迫使总统及其内阁辞职。只有当总统及其内阁高官违反宪法或渎职、失职时，国会才能对其进行弹劾。在西方主要国家中，虽然有总统的国家有好几个，但属于总统制国家的只有美国。无论议会制国家还是总统制国家，实质上都受到政党政治的主导和控制。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;总之，《六个为什么――对几个重大问题的回答》一书告诉我们：“三权分立”即使在西方国家也很少被采用，政党政治在西方国家中的作用至关重要。我国是社会主义国家，必须始终坚持党的领导、人民当家作主、依法治国有机统一，坚持和完善人民代表大会制度、中国共产党领导的多党合作和政治协商制度、民族区域自治制度以及基层群众自治制度，不断推进社会主义政治制度自我完善和发展，绝不能搞在西方国家也很少采用的“三权分立”。</div>

							
							
							
							
