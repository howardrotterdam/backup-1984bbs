<br />
2009年12月10日14:19中国新闻网 <br />
<br />
华工基因实验班学生论文登顶《自然》；实验班项目推动者王小宁炮轰当下教育模式――<br />
<br />
“现在大学很多基础学习是在浪费时间”！华南理工大学和深圳华大基因研究院合办的基因实验班学生取得人类基因组研究的重大突破，发现了人类基因组新成员，并首次提出“人类泛基因组”概念。该论文刊登在国际顶尖科技杂志《自然》(《Nature》)上(详见昨日头版《华工本科生发现人类基因组新成员》)。昨日，该实验班项目推动者、华工生物科学与工程学院院长王小宁接受采访时，提出不少新颖的教育理念。<br />
<br />
《自然》、《科学》杂志是科学界的权威杂志，每年在上面发表的国内文章不超过10篇。学界公认，能在上面发表文章，代表了在一部分科研领域的一流水平，至少是国内一流水平。<br />
<br />
作为在校大学生，基础课程还没学完，怎么就在很短时间内做出国际一流的学术成果？对此，王小宁没有一点意外。他认为，现行教育模式中，很多基础知识的学习是没有必要的，学与不学并不影响对顶尖科学问题的钻研。并且他认为，要培养一流创新型人才，一开始就应该将学生“推”到最前沿的科研项目中去。<br />
<br />
“目前大学教育强调‘厚基础、宽口径’，我不太认同。”他说，师生被“绑架”在课堂上，上了很多垃圾课，但许多基础知识学了根本用不上，反而把人变得迂腐，惯式思维影响创新能力。<br />
<br />
“教育出不了优秀的人才，是教育模式存在问题。”王小宁赞同中国科技大学前校长朱清时说过的话：进校时都是精英，出来时一半是笨蛋。王小宁说，目前国内许多高校的创新班，都已沦为了变样的学历教育，方便更快拿到博士，没起到真正的改革试点作用。<br />
<br />
这次在《自然》杂志发表论文的并列第一作者、华工大三学生罗锐邦为了自己心爱的科研就经常逃课，但他成绩很优秀。而另一位并列第一作者，华工毕业生郑汉城，在校时曾有几门功课不及格，个别课程的缺陷并没影响他超强的科研能力。<br />
<br />
（ 本报记者 夏杨 通讯员 刘慧婵）</div>

							
							
							
							
