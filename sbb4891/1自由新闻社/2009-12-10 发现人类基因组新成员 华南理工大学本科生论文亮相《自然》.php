													<h2>发现人类基因组新成员   华南理工大学本科生论文亮相《自然》</h2>
						
						
																											<div id="postmessage_366826" class="t_msgfont">华工本科生发现人类基因组新成员<br />
<br />
<img src="http://img1.cache.netease.com/catchpic/4/40/4000373FFD74BBABAFFC1FF409680246.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　本报讯记者夏杨报道：人类基因研究获得重大进展，论文的第一作者竟是广东在校大学生！昨日，记者从华南理工大学获悉，该校和深圳华大基因研究院合办的创新班在研究中出了惊人成绩：发现人类基因组中存在种群特异甚至个体独有的DNA序列和功能基因，并首次提出了“人类泛基因组”概念。<br />
<br />
　　在基因研究中，科研人员运用第二代测序技术和自主研发的基因组组装工具，对“炎黄一号”基因组―――首个亚洲人个人基因组进行了进一步的深度测序和拼接，发现人类基因组中除原先公认的单核甘酸多态性、插入删除多态性和结构性变异以外，还存在着种群特异甚至个体独有的DNA序列和功能基因，例如主要在亚洲人群内特有的基因序列。<br />
<br />
　　科研人员同时对近两年发表的非洲人基因组和韩国人基因组进行了重新组装，也得到类似结论。科研人员还首次提出了“人类泛基因组”的概念，即人类群体基因序列的总和。<br />
<br />
　　国际人类基因计划基于欧洲人DNA完成的参考基因组序列，是目前绝大多数人类基因组学研究的数据基础。多年来，大多数科学研究都认为每个个体的基因组均与这一参考基因组相似，仅有替换或重排性质的变化。<br />
<br />
　　专家指出，这一研究树立了新的人类基因组测序标准，进一步证明自主构建中国人群医学基因组学图谱、推进个人基因组研究和个体化医学研究的必要性，是中国科学家在人类基因组研究领域的又一重要贡献。<br />
<br />
　　该项研究由深圳华大基因研究院领衔，华南理工大学参与研究。研究论文《构建人类泛基因组序列图谱》前天已在国际著名科学期刊《自然》的生物技术分刊上发表。<br />
<br />
<br />
　　令人惊叹的是，这一重大学术成果的第一作者罗锐邦（见图，右）是华南理工大学创新班大三学生，今年只有20岁。这个基因组计划研究团队的平均年龄不到25岁，罗锐邦是年龄最小的。另一署名作者金鑫（见图，左）也是华工创新班大四学生。<br />
<br />
　　据华工介绍，该校和深圳华大基因研究院在今年初签署“基因组科学人才联合培养协议”，双方联手组建了“基因组科学创新班”，为优秀学生提供个性化培养，并探索产学研一体化的多模式合作办学新路。首期有10名大三学生入选。“创新班”学生前两年半在学校完成学业，后一年半在深圳华大基因研究院完成学业。 <br />
<br />
(本文来源：羊城晚报 )<br />
