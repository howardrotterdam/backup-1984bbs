<br />
　　三十多位民众4月1日来到北大抗议，纷纷表示要与孙东东理论。当他们抵达北大东门，现场很快来了许多警察和北大保卫处人员戒备，强行阻止他们进入。周莉等人虽然进入了校内，但很快被北大保卫处人员强行拖出来。<br />
<br />
