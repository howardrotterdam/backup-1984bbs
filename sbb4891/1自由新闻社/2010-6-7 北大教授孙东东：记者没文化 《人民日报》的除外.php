																											<div id="postmessage_658157" class="t_msgfont">2010年06月06日09:08<br />
南方网&nbsp;&nbsp;唐波<br />
<br />
<img src="http://img1.cache.netease.com/catchpic/6/66/66D8A924D4C6DF2AC939D89841A7A2AF.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
孙东东雷语：<br />
<br />
“是记者没文化，你去找记者，别找我。”<br />
<br />
“躺着进，竖着出，这不是正常的；竖着进，躺着出，才正常。”<br />
<br />
“记者没文化，当然，人民日报的除外……”昨天，由中国医院协会、人民网及江门市人民政府主办的 2010中国医院院长论坛在广东江门召开，来自全国各地近200名医院院长及数名专家学者与会。其间，北京大学教授、北大司法鉴定室主任孙东东在解读即将施行的《侵权法》时多次炮轰记者无文化，雷语不断，引得场下唏嘘一片。<br />
<br />
“记者没文化，人民日报的除外”<br />
<br />
昨日，孙东东教授可谓全场最为特别的一位与会者。数天前，孙教授在一场交通事故中受伤，至今还未痊愈。因此，在昨天的演讲中，他的左胳膊仍然打着绷带。孙东东的专题演讲，主要是针对今年7月 1日即将施行的《侵权法》中有关“医疗损害责任”条款的解读。<br />
<br />
演讲伊始，孙教授便举了一个例子剑指“记者无文化”。他说，曾经有一次接受中央电视台专访，解读关于全民医保的问题。“我从来没说过中国在××时候能实现全民医保，这也是现今不可能的事情，没想到第二天，报纸一出来，就这样误读了。”孙东东说，他后来接到相关部门负责人的质问电话，无言以对，只得告知“是记者没文化，你去找记者，别找我”。随后，孙东东对着台下人民日报社的记者说“记者是没文化，当然，人民日报的除外”。<br />
<br />
“医院要死人才正常”<br />
<br />
“记者无文化”论引得台下唏嘘一片，然而，孙东东随之又抛出了“死人正常论”，台下当即笑声一片。<br />
<br />
在谈到医疗行业的风险时，孙东东告诫各位院长一定要树立风险意识，做好告知义务，因为在众多的医疗纠纷中，大部分都是因为告知不到位所导致的。<br />
<br />
孙东东在演讲中表示，“躺着进，竖着出，这不是正常的”，而“竖着进，躺着出，才正常”。他还表示，之前的那句口号“救死扶伤、施行革命人道主义”是不对的，正是因为这样的宣传，导致医患纠纷越来越多。他建议院长们，应该多宣传“死人是正常的”，多给患者和亲属讲清不利的方面。<br />
<br />
昨天上午，包括上海新华医院、郑州金水总医院、江门市人民医院等医院的院长分别作了专题演讲。此外，卫生部新闻发言人毛群安、清华大学医疗健康管理研究中心教授周生来等还分别就医院与媒体打交道、现代化的医疗健康体系等问题，作了专题演讲。今天上午，论坛组委会将邀请部分院长参加公立医院改革座谈会，届时，卫生部等有关部门的领导，将出席会议并听取与会代表的意见。<br />
<br />
(记者唐波)</div>

							
							
							
							
