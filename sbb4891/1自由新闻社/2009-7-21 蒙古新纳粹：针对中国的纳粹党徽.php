													<h2>蒙古新纳粹：针对中国的纳粹党徽</h2>
						
						
																											<div id="postmessage_171190" class="t_msgfont">美国时代杂志7月27日文章（提前出版）The Neo-Nazis of Mongolia: Swastikas Against China（蒙古新纳粹：针对中国的纳粹党徽），作者Mitch Moxley，摘译如下：<br />
<br />
　　在蒙古首都乌兰巴托一座电影院附近的砖墙上，有喷漆而成的“射死中国人（Shoot the Chinese）”字样。在蒙古包群落外围，围墙上可见“杀手男孩……！危险！”的字样，以及一对纳粹党徽。像这样的涂鸦在全市处处可见，这是蒙古新纳粹的作品。蒙古新纳粹，这是一个令人难以置信、令人害怕、有时候又是暴力的运动。<br />
<br />
　　乌兰巴托有三个极端民族主义组织，它们宣称成员加起来有几千人――在这个人口仅三百万的国家，这可是不小的数字。它们采用纳粹的用具和教条，而且强烈反华。一个叫蓝色蒙古（Blue Mongolia）的组织已经承认，它会对那些被发现和中国男人睡觉的当地女子实施剃头。它的领袖去年承认杀死了女儿的蒙古男友――那位男友只是曾在中国学习而已。<br />
<br />
　　新纳粹可能处于社会边缘，但他们代表着一股非常真实的民族主义潮流的极端。蒙古夹在俄罗斯和中国之间，外国势力叫嚣着分割蒙古的矿产财富，很多蒙古人担心遭到经济上和种族上的殖民。这促使他们展示出对外界的敌意，并导致重要外国投资谈判进度的缓慢。<br />
<br />
　　五十岁的Zagas Erdenebileg是最突出的新纳粹组织达亚尔蒙古人（Dayar Mongol，全体蒙古人）的领袖。他四次竞选议会都以失败告终。他表示，“如果我们的血与外国人混合，我们将立即被消灭。”他憎恨中国人，认为中国人参与卖淫和贩毒活动，他崇敬成吉思汗，认为成吉思汗影响了希特勒。<br />
<br />
　　如果说Zagas Erdenebileg是蒙古新纳粹的老政治家，那23岁的Shari Mungun-Erdene就是新手，他是大约200多成员的蒙古民族联盟（Mongolian National Union）的领袖，他的胸部有一个惹人注目的纳粹党徽纹身。Mungun-Erden称，他的组织对违法的外人（主要是中国人）采取义务警员行动。当我问他采取何种行动时，他回答说，“只要能让他们不在这里生活，什么都行。”<br />
<br />
　　乌兰巴托的开放社会论坛是一个促进公众参与公民生活的组织，该组织的项目经理Dagva Enkhtsetseg指出，新纳粹并不享有广泛的支持。她认为，随着越来越多年轻蒙古人受到全球化的影响以及到海外学习，强硬民族主义的吸引力在减弱。明显的例子是，在五月的总统选举期间，那些关于民主党领袖及最后赢家Tsakhiagiin Elbegdorj有中国血统的虚假指责无人关注。Dagva Enkhtsetse表示，在过去，那种指责会起到效果。<br />
<br />
　　但新纳粹仍然构成一些威胁。在五月，国际发展慈善组织海外志愿服务机构的通讯报道说，两位和平队（Peace Corps）志愿者和达亚尔蒙古人成员发生对峙，在一家酒吧外面遭暴打。<br />
<br />
&nbsp; &nbsp; 监视中国在亚太地区的军事活动，这是印度“包围”这个与它比邻的核竞争对手的更广泛战略的一部分。为做到这一点，印度正逐步与蒙古加强国防、安全方面的合作。<br />
<br />
&nbsp; &nbsp; 为进一步加强这方面的合作，由4名印度陆军上校组成的一个小组参加了在蒙古首都乌兰巴托举行的&quot;可汗探索-2007&quot;战地指挥所演习。此次军演已于8月结束。<br />
<br />
&nbsp; &nbsp; 国防部门消息人士说，在战地指挥所演习后，印度和蒙古军队将于今年晚些时候在乌兰巴托以西65公里处的一处维和训练中心举行两国间的第三次联合军演。<br />
<br />
&nbsp; &nbsp; 印蒙两国曾于2004年10月在蒙古举行联合维和行动演习。一年后，双方又在印度东北部米佐拉姆的“反叛乱和丛林战学校”举行了第二次联合军演。当时的蒙古国防部长策伦呼・色拉布道尔吉观摩了演习。那时恰逢印蒙两国建交50周年。两国于1955年12月建交。在色拉布道尔吉2005年访问印度期间，双方成立了联合工作小组。此前，两国曾于2001年签署《国防合作协议》，包括举行联合军事演习、两国军事官员互访等内容。<br />
<br />
&nbsp; &nbsp; 自20世纪90年代末新德里实行“向东看”政策以来，新德里与乌兰巴托的关系一直在稳步发展。“向东看”政策旨在培养印度与中国的邻国、尤其是东盟国家之间的国防和战略关系。<br />
<br />
&nbsp; &nbsp; 2004年1月，印度航天部与蒙古基础设施部签署了合作议定书。印度根据这一议定书在蒙古设立了“监听站”。军方消息人士说，如今，根据已有协议，印度打算扩大和改进它在蒙古设立的这些“监听站”。<br />
<br />
&nbsp; &nbsp; 该议定书为印蒙两国在空间科学技术等领域开展合作提供了“保护伞”。它还涉及与卫星通信、卫星遥感及卫星气象学有关的研究领域。合作议定书还包括卫星地面站、卫星发射管理、训练设施以及科学家交换等内容。<br />
<br />
&nbsp; &nbsp; 印度还考虑在蒙古某些地方设立预警雷达，这些雷达将使印度能够监视中国在广大沙漠地区进行的导弹试验以及北京不断扩大的太空项目。<br />
<br />
&nbsp; &nbsp; 官方消息人士说，除此之外，印度还打算扩大驻乌兰巴托的大使馆，向蒙古增派安全部门人员和军事人员，以管理这些拟议中的资产。不过，他们拒绝披露更多详细情况。（原标题：印度与蒙古加强国防、安全合作以加紧监视中国）<br />
<br />
<br />
