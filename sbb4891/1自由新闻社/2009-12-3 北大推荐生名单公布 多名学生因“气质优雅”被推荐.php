核心提示：近日，北大招生网新公示的北大推荐生名单及中学校长推荐理由引来不少人关注，湖北武汉外国语学校一女生的评语是“外校才女初长成，隐隐已有大家气……是校园的一枝百合花”。<br />
重庆晨报12月3日报道 近日，北大招生网新公示的北大推荐生名单及中学校长推荐理由引来不少人关注，细心的读者发现，在推荐理由中，大多学生的评语都写有“学习成绩优秀”、“品学兼优”、“奥赛获奖”、“素质超强”，<strong>但湖北武汉外国语学校一女生的评语竟是“是校园的一枝百合花”，这不禁让人议论纷纷，甚至有网友发帖《你气质优雅吗?优雅你就能上北大了!》质疑“校花”成为推荐上北大的理由。</strong><br />
<br />
七成推荐生是“优生”<br />
<br />
记者注意到，在北大公示的90名推荐生中，有近七成的推荐理由都有“成绩优异”。我市巴蜀中学、南开中学推荐的6位学生中，有3位学生得到的评语几乎一致：一是综合素质优秀，二是学业成绩好，三是年级综合排名靠前;另外3位学生，在以上三点的基础上还加入了一些竞赛获奖情况和兴趣爱好介绍。<br />
<br />
记者了解到，重庆推荐的学生从公示以来，学校没有接到任何质疑的信息。<br />
<br />
质疑“校花”上北大<br />
<br />
<br />
记者仔细梳理了一下北大招生网上公示的90名推荐生的推荐评语，除了“成绩优异”、“名列前茅”、“素质超强”等评语外，也不乏一些个性化的评语：<strong>北京四中楼悦晨同学的评语中有“气质优雅，性格稳重，思想深邃”的句子;广东华南师大附中的梁昊评语中展示了他在5年的股市投资实践中一直保持着40%盈利;湖北武汉外国语学校李琬同学的评语中有“外校才女初长成，隐隐已有大家气……是校园的一枝百合花”的句子;江苏省泰州中学周鹏的评语中还出现了“外表阳光”的字眼。</strong><br />
<br />
对于这些充满了个性的评语，一些人心生嘀咕：“这样的推荐评语，更像是征婚广告用语，或者是选美比赛的评语。”<br />
<br />
