各位能找出下面三段话的不同之处吗？<br />
<br />
“热爱集体，乐于奉献，思维敏捷，勤于钻研，勇于创新，敢于质疑，成绩优异，竞赛成绩突出，兴趣广泛，多才多艺，善于合作，积极参加多项社会工作，组织能力强，综合素质突出，在同学和教师推荐过程中得到高度认同”。 <br />
<br />
“热爱集体，乐于奉献，思维敏捷，学习能力强，勤于钻研，勇于创新，学习成绩优异，多次获得年级第一，善于合作，社会工作能力强，综合素质突出，能够发挥表率和带动作用，在同学和相关教师推荐过程中得到高度认同”。<br />
<br />
“热爱集体，乐于奉献，思维敏捷，勤于钻研，勇于创新，成绩优异，多次获得年级第一，兴趣广泛，文理兼优，尤其对人文社会科学有着特殊的爱好，善于合作，组织能力强，综合素质突出，在同学和教师推荐中得到高度认同”。<br />
<br />
这不是“大家来找茬”的游戏，而是北大附中校长实名推荐三名保送生的评语。仅从字面上看，三段评语就像无性繁殖的草履虫，一分为三，无大不同：集体总是热爱的，奉献总是乐于的，思维总是敏捷的，素质总是突出的，认同总是高度的。可是，我们完全不能看到被推荐者的个性与才能，在死气沉沉的推荐语里，他们就像同一模版浇铸出来的蜡像馆小人儿，连微笑都是一样的长度。<br />
<br />
回忆一下我们的学生时代，在所有推荐语或教师评语一栏里，我们得到的评语是不是跟文初所举的大同小异呢？在死气沉沉的推荐语里，折射出的是流水作业的刻板，应试教育所制造的伤痕。这伤痕不止烙在学生身上，也烙在教育者身上。对学生而言，才能与个性在刻板应试教育中被掩埋、被忽略、被雪藏，乃至被毁损；对教育者而言，习惯摧残或无视孩子的个性与才能，久而久之，自己也相应地失去个性和才能。<br />
<br />
或有人说，推荐语就是走过场，用得着这么上纲上线吗？不然。在西方教育体系里，推荐语是极其重要的东西，评者必须对之负全责，而被评者的成功与否也与之有密切关联。它体现的是一种公平公开公正的教育评价体系，而个性和才能在此种评价体系中则能得到最大善意的对待。某种程度上，推荐语是对玉璞的有力琢磨，是掀开布囊露出锥的锋芒，是让千里马自围栏中脱缰而出。它不但是推荐者对实名推荐制的一种严肃的责任担当，更是导师对所推荐者的一份洋溢理性与温情的剖析、赞成与建言。<br />
<br />
如果允许我们放开历史的视野，不妨将实名推荐与与汉末的月旦评相提并论。二者的行政功能虽不一致，但其爱惜人才、懂得人才、荐举人才的功能是一致的。然而，相比昔日月旦评的绝代风华而言，今日的推荐语却是多么的乏善可陈。明人程登吉云：“言辞不爽，谓之金石语；乡党公论，谓之月旦评”。手握实名推荐权的中学校长们，愿你们对学生有发自内心的热爱与细若发丝的了解，并由之生产出坚如金石、允为公论的量身定做的推荐语！</div>

							
							
							
							
