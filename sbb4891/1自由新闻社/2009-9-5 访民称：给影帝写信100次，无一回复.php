新老访民受到严格限制 <br />
<br />
中国每年有1150万人到北京上访，他们只是为了陈述冤情，讨回公道。在人民共和国成立六十周年即将到来之际，中国政府正在驱逐老访民出首都，并严密控制新访民进京。《明镜周刊》注意到中国政府对上访问题的新规定和新做法： <br />
<br />
&quot;新的原则方针是，今后访民不应上访中央政府，北京将派官员到省区视察。地方当局必须每月抽出一天时间，聆听取公民的要求。同时，政府在首都加强了检查。出租汽车司机显然已获得指示，要举报可能是上访人员的乘客。此外，南站周围的房主不得向这些寻求援助的人出租房屋。去年奥运前不久，政府拆除了一个住有许多访民的居住点。<br />
<br />
北京理工大学经济学家胡星斗认为，这些措施无法阻挡访民涌来，他们很天真，相信中央政府会帮助他们。如去年四川地震后那样，自从Wen Jiabao总理以亲民的父母官形象出现以来，访民希望只要能引起最高层某人的重视，他们的问题就能得到满意的解决。这样的希望往往达到着魔的程度，一些家庭为了递交上访信，在北京一待数年，有时数十年。&quot;<br />
<br />
那么，地方当局能为访民伸张正义吗？《明镜周刊》以一位王姓访民为例，回答了这个问题：<br />
<br />
&quot;他象重罪罪犯一样被人带到派出所，只因为他大声骂了地方官员。他想向区政府报告警官殴打他的情况时，警察威胁说：想想你孩子的政治评语！他先到本市的信访办，一无所获。接着去河南省信访处，但家乡的捕快们已在那里等候，所以他前往北京。当他在中央信访局排队时，几名和气的男子朝他走来，答应提供住处和帮助。他们把他带到'青年旅社'，先关押起来，然后与其它访民一起，押送回乡。<br />
<br />
中国维权民生观察组织说，北京确实有这种地方政府在廉价客店或驻京代办处地下室设立的'黑监狱'。那里，往往数十名访民挤住在一间屋内，睡在地下，忍饥受饿，'受到猪狗一样的待遇'。甚至《中国日报》也报道说，不久前，一名女访民在这样一个住处受到强暴。<br />
<br />
地方当局想防止中央政府获悉访民控告的事情。上访人数多就意味着党内评审政绩时得不到好分数。上访告状的人回家后都担心会受到地方官员的报复。经济教授胡星斗调查后得出结论：45%接受问询的人曾被关押，13%的人被送往劳教。此外，访民们还报告说，他们被强行送入精神病医院，财产被没收，住宅遭到破坏。<br />
<br />
所以，姓王的访民不敢回家，他要留在北京，直到他获得公道为止。但他现在不再亲自去信访局，只向当局邮寄信件，他也给Wen Jiabao写信，已写了整整一百次，总理没有回复。&quot;<br />
