																											<div id="postmessage_508804" class="t_msgfont">王奉春“被开除”急病父亲 男子冰壶队长盼回归<br />
<a href="http://sports.sohu.com/20100308/n270661663.shtml" target="_blank">http://sports.sohu.com/20100308/n270661663.shtml</a><br />
<br />
<br />
<img src="http://photocdn.sohu.com/20100308/Img270661849.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
本报讯（记者谷欣）“被开除”事件急病了老父亲，中国男子冰壶队队长王奉春在反省中等待国家队的召唤。<br />
　　首次亮相冬奥会，便分别取得第八和第三名的中国男、女冰壶队昨天正式放假，队员们集体乘火车返回哈尔滨。随大部队一起回家的，还有身处“离队”风波中的王奉春。<br />
　　在中国冬奥代表团回国当夜，王奉春正准备享用国家体育总局冬季运动管理中心安排的庆功宴，突然被领队李东岩通知离队，失魂落魄的他打车到了火车站，却因为不想让父母担心而决定悄悄留京。<br />
　　“当时心里很难受，也不想让父母知道。因为我和他们说的是过几天再回去。所以我又打车离开了火车站，不过没回队里，而是自己找了个地方住下。”王奉春昨天在电话中对记者说。<br />
　　王奉春的这一决定，让事态有了转机。第二天，他便接到地方队教练的电话，转达了国家队让他参加3月5日国家体育总局在北京举行的庆功宴的决定。<br />
　　在庆功宴当天，王奉春将行李搬到首体宾馆，重新和大部队集合的同时，也和领队李东岩进行了一次长谈。<br />
　　“这几天，我一直在进行反省。但我对冰壶事业的热爱从来未曾改变，我非常渴望能够重返冰壶赛场，为国家效力。”王奉春如此向记者转述自己与领队沟通的内容。<br />
　　虽然是否留队一事已有转机，但王奉春昨天才知道，家中因这场风波而出现了变故。<br />
　　原来，就在王奉春悄悄留在北京反省的这几天，他被“开除”的新闻早已经被哈尔滨媒体报道了出来。一向身体就不太好的父亲，因担心儿子而突发心脏病，被送到了医院。<br />
　　“之前我一直瞒着自己离队的事，没想到家里人早就知道了，他们还瞒着我父亲住院的消息。”王奉春声音一度哽咽，内疚地说。<br />
　　对话主角<br />
　　王奉春：等待征召渴望再战冬奥会<br />
　　昨天，记者通过电话采访了已回到哈尔滨的王奉春。他表示，自己确实有做得不对的地方，这两天一直在反省。同时，他也急切等待着被国家队重新征召入队的消息。<br />
　　FW：和冬奥代表团回国当晚，你说自己不知道为什么就被队里开除了，现在知道原因了吗？<br />
　　王奉春：当时领队给我火车票突然让我回家，我的情绪比较激动。不过这几天，我一直在进行反省，自己在冬奥会比赛期间，或者生活中的细节方面，确实有做得不妥的地方。我想，这也是以后我需要不断完善和改进的。<br />
　　FW：能具体说说吗？<strong>比如，你回国时为什么要戴一顶美国队的帽子？</strong><br />
　　王奉春：代表团回国那天的场合，我不应该戴美国队的帽子，这也是我在细节上考虑不周。<br />
　　其实，这顶帽子是我们在冬奥会和美国队比赛结束后，一个美国队员主动和我交换的。因为我们这几年一直和他们交手，以前我们打不过他们，但现在我们成绩提高很快。在赛场外我们像朋友一样交流，所以在奥运会上交换帽子做个纪念。<br />
　　FW：你认为自己在冬奥会表现如何？<br />
　　王奉春：这是我第一次参加冬奥会，暴露了大赛经验不足的缺点。但通过这次冬奥会的历练，我也从其他强队学到了很多东西，得到了锻炼。我想，这也是冬奥会给我的一笔财富。<br />
　　FW：国家队通知你何时归队了吗？<br />
　　王奉春：我们现在刚刚放假，4月初还有世锦赛要比，目前大家都在等通知。<br />
　　FW：你觉得自己重返国家队的机会大吗？<br />
　　王奉春：从我的角度，我是迫切想回去的。这么多年来，我对冰壶的热爱从来没有改变过。如果国家需要我，我会义不容辞。我很希望用自己的经验继续为国家效力，能够给我机会打下一届冬奥会，我相信自己会发挥得更好。<br />
　　文/记者谷欣<br />
