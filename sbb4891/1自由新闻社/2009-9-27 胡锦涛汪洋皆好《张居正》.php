<br />
张居正（ 1525年─ 1582年），汉族，原籍湖广江陵（今属湖北）人。字叔大，少名张白圭，又称张江陵，号太岳，谥号「文忠」。明代后期政治家、改革家，中国历史上最有名、也是最具争议的首辅（宰相）之一。<br />
