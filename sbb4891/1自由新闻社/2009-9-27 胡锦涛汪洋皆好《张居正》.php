<br />
他主政 10年，推行系列改革措施，为明朝苟延残喘立下汗马功劳，但也得罪不少人，包括万历皇帝，故死后被削尽官名，差点开棺戮尸。<br />
<br />
但后世研究多认为，张改革的目的是为缓当时社会矛盾，巩固明朝统治。措施虽有效但得罪利益集团，故死后被反攻倒算，改革成果毁于一旦。学术界将先秦时的商鞅、宋朝的王安石和他，分列为中国封建社会初期、中期和后期三大改革宰相。<br />
