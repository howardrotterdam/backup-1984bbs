													<h2>尽论中国：塑造张居正　营造统治合法性</h2>
						
						
																											<div id="postmessage_569388" class="t_msgfont">Apple Daily 苹果日报网上版 －－2010-04-18<br />
尽论中国：塑造张居正　营造统治合法性<br />
<br />
电视剧《万历首辅张居正》尘封四年后重现内地荧屏，无论是剧情的改编、画面的删剪，都使张居正的形象更「正面」，少了贪财好色的成份，多了改革者的光环，但连导演苏舟也慨叹电视剧不知最终能播出几集，张居正的扮演者唐国强更说：「假如过几年审查政策更宽松了，这部剧应该再拍一遍，把那些被删减的戏都重新放进去，弥补今天的遗憾。」<br />
<br />
随意窜改历史，依据政治需要去塑造历史人物，在中共的电影、电视中屡见不鲜，问题只在于中共想藉塑造张居正表达甚么？内地一些独立学者、专家指出，张居正重循吏、贬清流，符合邓小平「不管黑猫白猫、抓到老鼠就是好猫」论，迎合既做官又发财的太子党的需要，投合胡锦涛等高官的心意。<br />
<br />
为官员腐败寻找合理借口<br />
<br />
看深一层，中共刻意吹捧张居正，更是为了营造其统治合法性的背景。当社会贫富分化、官民冲突此伏彼起时，一个一党专政的政权，其合法性难免受到更多质疑、挑战。统治者赖以支撑其合法性的惟有政绩，包括国家主权的完整和独立、经济持续发展、官员的道德操守等，而内地官员操守的崩溃，已成为最薄弱、最致命的环节。<br />
<br />
在政制改革停滞不前时，当局深知反腐倡廉只是空谈，要纾缓民怨，就要为官员的腐败寻找既合理又隐蔽的借口。既是伟大的改革者，又纵容同盟者敛财的张居正，显然因此被中共高层看中。只不过，主角如果太过贪财好色，会有损宣传效果，电视剧的删剪也就不在话下。<br />
<br />
张居正的为官哲学是：「野有饿殍，你纵然餐餐喝菜汤，也算不得一个好官。如果老百姓丰衣足食，你顿顿吃肉，笙歌不绝于耳，你依然是一个好官。」如此高论，不只深得内地官员之心，在民间也有一定市场。一旦中国的民众接受了这套理论，中共就能以经济的增长掩饰官员的腐败，延伸政权的合法性。这个如意算盘，真的打得响吗？<br />
