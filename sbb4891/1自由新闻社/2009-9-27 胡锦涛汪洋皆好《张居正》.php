剧中万历皇帝的母亲李太后，由梅婷饰演。<br />
<br />
其时明朝内政腐败，外患不断，危机四伏，加上小皇帝贪财好色，终日享乐，江山摇摇欲坠。张居正救亡图存，全力拯救明朝将倾之厦。为选贤拔才，他推出京察（即考评）制度；为解国库不足，他用胡椒等香料当粮支付官员。因明朝官员薪俸很低，张默许官员参加赌博；又不顾争议，重用有才的贪官。他还强推新税赋制，得罪不少豪门及地方势力，为自己死后遭报复埋下种子；他查办贪污，一度追究到小皇帝身边。<br />
