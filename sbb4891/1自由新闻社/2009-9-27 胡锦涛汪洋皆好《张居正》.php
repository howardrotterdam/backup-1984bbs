													<h2>三大电视台同播《张居正》歌颂忠君思想</h2>
						
						
																											<div id="postmessage_569387" class="t_msgfont">Apple Daily 苹果日报网上版 －－2010-04-18<br />
三台齐播争议首辅历史剧　歌颂忠君思想<br />
胡锦涛捧张居正做伟人样板<br />
<br />
 <img src="http://pic1984.12hp.de//up/1271559018.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
历史剧《万历首辅张居正》由唐国强（中）饰演张居正，宣传海报公开声称，该剧是「借古喻今」彰显改革的力作。<br />
<br />
内地以年产一万五千部（集）电视剧之势，跻身世界影视生产大国之首。不少剧集以讲故事的娱乐方式，针砭时弊、引领话题，给世界华人观众留下深刻印象。北京大学教授张颐武表示：中国人需要新闻，更需要娱乐，因为新闻很受限制，娱乐更能让中国人充份感受生命的价值。内地近日推出的历史剧《万历首辅张居正》，由于备受中共总书记胡锦涛推崇，再度成为舆论的焦点。<br />
<br />
中央电视台、上海东方电视台和北京电视台前晚同时开播 43集历史剧《万历首辅张居正》（简称《张居正》）。该剧四年前已拍成但一直被雪藏，现三大电视台同时推出，据悉与中共总书记胡锦涛推崇有关；传胡亲自调看该剧；广东上海等地方高官跟风推崇。有学者指，高层希望各级官员如张居正般忠君为朝廷，官员则期待上头如张居正般容忍贪腐。<br />
<br />
《万历首辅张居正》以明代万历年间传奇首辅张居正为主题，除了讲述他如何改革朝政外，亦加入了不少感情戏。<br />
<br />
官员需读有关小说<br />
<br />
央视等三大电视台同步推出根据同名小说改编的《张居正》剧集，媒体也造势大事报道，显得格外隆重。但昨日即有媒体轰剧中的主角张居正「形象高大全（即高尚、伟大和全面），不如改名叫《大明公仆张居正》」；导演苏舟出面解释，指该剧「重在正面描写张居正历史功绩，要把不利当代社会的负面影响消除」。<br />
<br />
电视剧《张居正》早于 2006年已制作完毕，但恰遇广电总局清理帝王将相等古装戏太多，因此遭雪藏。但去年中北京传出，中共总书记胡锦涛力捧张居正，胡办工作人员兴起读小说《张居正》，甚至每人手上拥一册，胡还亲自调看了该剧。<br />
<br />
据悉，胡锦涛所好影响到地方官员。广东省委书记汪洋也跟着「喜爱张居正」，要求广东官员阅读有关小说……。还有消息指，上海市委书记俞正声曾接见小说《张居正》的作者熊召政，一起「感悟张居正」。<br />
<br />
张居正不提倡清廉<br />
<br />
内地学者朱建国对本报指，三大电视台同推《张居正》，证实该剧得到中共上层推崇，「胡锦涛的意思很明确，就是要各级官员学习张居正，为中共问题百出的江山匡时济世。」朱指，中共近年一直从封建历史中寻找治国方法，例如宣扬孔孟思想、推崇张这样的人物榜样，透露他们的改革底线就是像封建时代那样，在朝廷准许范围内改革。<br />
<br />
小说《张居正》作者熊召政也指，历史上的张居正并非清官，张主政也不提倡清廉，相反他采用循吏（即由着官员）的实用政策，就是中共已故领导人邓小平所倡导不管白猫黑猫，逮住老鼠就是好猫。官员只要有能力就重用，不管有无贪腐或不法；只要对维持政权有利，任何暴政都不会拒绝。<br />
<br />
朱建国指，看到胡锦涛如此喜爱《张居正》，官方媒体又大事宣传张居正，各级贪官污吏们应该放心了，因为张虽治国手段残酷，又贪财好色，但只要有政绩，而且忠君爱主，仍可名垂千古，「这大概也是高官拚命追随主子，推崇张居正的原因吧」！<br />
