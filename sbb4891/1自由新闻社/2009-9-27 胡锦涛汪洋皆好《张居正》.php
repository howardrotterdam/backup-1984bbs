《张居正》剧集由中央电视台和湖北长江出版集团等联合拍摄，耗资 2,400万元人民币。讲明朝末年隆庆皇帝久病，张居正（唐国强饰）奉召入宫辅政，不久隆庆驾崩，万历小皇帝（即朱翊钧，梅年佳饰）登基；张居正因做万历的老师，得以首辅身份，接掌朝政。<br />
