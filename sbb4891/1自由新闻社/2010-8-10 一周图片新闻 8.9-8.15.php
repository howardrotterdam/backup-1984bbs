													<h2>江湖中人郭德纲</h2>
						
						
																											<div id="postmessage_780215" class="t_msgfont">.<br />
<br />
<br />
<br />
<br />
.<br />
<br />
<br />
<br />
<br />
<br />
.　<br />
<br />
<br />
<br />
　　<img src="http://img3.cache.netease.com/photo/0001/2010-08-11/900x600_6DR290MF3R710001.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　2006年2月9日，郭德纲和搭档于谦在北京天桥剧场举行专场演出。郭德纲14岁开始在天津红桥区文化馆工作，1989年，他经人介绍加入天津曲艺团，正式成为专业相声演员，月工资100元。1995年他放弃工作，从天津到北京北漂，混过电视剧组、参加过民间小剧团，甚至还睡过西红门的桥洞。之后他开始与张文顺等人在京味茶馆等地说相声、评书，并于1996年成立&quot;北京相声大会&quot;，每位观众只收2元钱，即便如此，能坐七八十人的茶馆里还经常没人，只好到大街上边说快板边拉顾客。<br />
<br />
<br />
<br />
　　<img src="http://img3.cache.netease.com/photo/0001/2010-08-11/900x600_6DR2869Q3R710001.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　2006年2月27日，北京天桥乐茶园外德云社售票处场面火爆，购票者一度失控，挤破了门口的玻璃门。2003年，&quot;北京相声大会&quot;更名&quot;德云社&quot;，郭德纲已经有了十几人的队伍，并改到天桥乐茶园演出，于谦也正式成为他的搭档。2004年举行&quot;失传曲目专场&quot;之后，来茶园的观众日益增多，从那时起，德云社才真正有了起色，开始盈利。当年，郭德纲曾在三天内接受150多家媒体的采访。在媒体对其&quot;井喷&quot;报道及网络媒体的推波助澜之下，郭德纲一跃成为现今相声界最著名演员之一。<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://img3.cache.netease.com/photo/0001/2010-08-11/900x600_6DR28QRH3R710001.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　2006年1月23日，郭德纲在《艺术人生》的录制现场畅谈自己的从艺经历。凤凰卫视也在春节特别节目中推出一连四场《郭德纲相声专场》，而郭德纲、于谦的北京新年相声大会专场更是场面火爆，一票难求。<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://img4.cache.netease.com/photo/0001/2010-08-11/900x600_6DR289B53R710001.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　2006年11月4日凌晨2点45分，随着长达7个小时的“相声长跑”结束，最后观众起立鼓掌达半个多小时，德云社十周年庆典在郭德纲多次返场表演后终于落幕。庆典开幕式当天，最低的50元门票就被黄牛党炒到了500元，6场演出净挣100多万。<br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://img4.cache.netease.com/photo/0001/2010-08-11/900x600_6DR296EV3R710001.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　2007年4月15日，北京新德云社开张，郭德纲站在新德云社的匾额下信心十足。这一年，郭德纲的名字首次登上福布斯中国名人榜，以年收入1000万元排名第29位。之后的2008年与2009年，他又分别以上年收入1570万元与1055万元排名第19位与第32位。目前，德云社已在北京拥有四家剧院。而在相声之外，种种嘴仗也让郭德纲的曝光率急速上升。<br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://img4.cache.netease.com/photo/0001/2010-08-11/900x600_6DR293KO3R710001.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　2006年4月19日下午，相声演员郭德纲召开媒体发布会，公开向汪洋及其家人道歉。此前，郭德纲的相声段子中的“汪洋的老婆跟别人睡觉，汪洋因此要自焚”和博客中的言辞将汪洋惹怒，两个幼年旧识就反目为仇。<br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://img4.cache.netease.com/photo/0001/2010-08-11/900x600_6DR28F3G3R710001.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　2007年的3・15晚会对于七宗不良事件进行了重点曝光，由郭德纲代言的“藏秘排油”也成为打假对象。3・15晚会晚会过后，郭德纲刻意以相声段子《谁动了我的减肥茶》回击央视。<br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://img3.cache.netease.com/photo/0001/2010-08-11/900x600_6DR280NV3R710001.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　2007年4月4日，郭德纲名誉权案在北京大兴法院开庭，法官做出郭德纲胜诉的宣判后，郭德纲和拄着双拐赶来旁听的忠实粉丝拥抱。2006年年3月，郭德纲发表博文提及其在天津红桥文化馆学艺时，师父杨志刚“整治郭德纲逼其扫厕所”和“与女同事同居”等若干作风问题。杨志刚后以“诽谤罪”将郭德纲告上法庭。<br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://img3.cache.netease.com/photo/0001/2010-08-11/900x600_6DR283GB3R710001.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　2009年2月22日，德云社元老张文顺的追悼会上，郭德纲和此前有过口舌交锋并退出德云社的徐德亮不计前嫌互相握手。徐德亮是德云社早期的重要成员和台柱，2008年9月19日，徐德亮发表声明宣布退出德云社。有分析称，由于经济收入方面与郭德纲有了天壤之别而得不到提升，徐德亮一直扮演着一个打工者的身份，出于生计而出走。<br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://img4.cache.netease.com/photo/0001/2010-08-11/900x600_6DR29G943R710001.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　2007年7月7日，郭德纲在北京八宝山哭送侯耀文。2007年6月23日，侯耀文因心脏病突发在北京家中去世。郭德纲2004年拜入侯耀文门下，2005年加入侯耀文所领导的中国铁路文工团。<br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://img4.cache.netease.com/photo/0001/2010-08-11/900x600_6DR28CB43R710001.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　2006年3月26日，侯耀文携徒弟郭德纲为在上海的演出造势，师徒不时耳语关系密切。2009年，郭德纲曝光侯耀文遗产问题，引发侯耀华与郭德纲之间的互损对骂步步升级。<br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://img3.cache.netease.com/photo/0001/2010-08-11/900x600_6DR28KUJ3R710001.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　2010年4月11日，情系旱区郭德纲反串京剧《法门寺》在北京举行赈灾义演。演出前的彩排上，郭德纲的干儿子“京剧神童”小陶阳给师傅捶肩。德云社目前是公司制运营，但郭德纲给老板和雇员的关系又赋予了一层师徒关系。<br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://img3.cache.netease.com/photo/0001/2010-08-11/900x600_6DR29J4M3R710001.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　2008年2月4日，郭德纲代表德云社将前一天演出所得的10万元捐助给遭受冰雪灾害的南方灾区后，接过中华慈善总会颁发的“慈善”水晶纪念品。2010年，章子怡身陷“捐款门”后，郭德纲力挺好友，称章子怡明显是被人陷害，“这事要出在我身上，要是知道是谁，我肯定要上门找他打架的。”<br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://img4.cache.netease.com/photo/0001/2010-08-11/900x600_6DR28NNR3R710001.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　从一名默默无闻的相声演员到成立德云社、拥有众多弟子、名利双收的著名相声演员，郭德纲的私人生活也备受媒体关注，此前他多次被拍到身穿皮草名车出行的样子。<br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://img3.cache.netease.com/photo/0001/2010-08-11/900x600_6DR2QIIS3R710001.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　2009年1月7日，郭德纲在北京西单图书大厦为出版的新书《郭德纲这点事儿》做签售，书中披露了他从出生到现在的成长与奋斗过程，其中郭德纲回忆了其艰辛的“北漂”生活。<br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://img4.cache.netease.com/photo/0001/2010-08-11/900x600_6DR29M3H3R710001.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　2009年12月6日，北京德云社三里屯店暨郭家菜馆、德云华服开业。郭德纲率众多弟子庆典迎客。郭德纲身穿缎面红衫，为自家德云华服做活广告。<br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://img3.cache.netease.com/photo/0001/2010-08-11/900x600_6DR299KR3R710001.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　2010年3月3日，在喜剧片《越光宝盒》中，郭德纲扮演曹操一角，并以女装造型亮相银幕，模仿《泰坦尼克号》露丝让杰克画裸像的情节。<br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://img3.cache.netease.com/photo/0001/2010-08-11/900x600_6DR28I633R710001.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　2010年1月，郭德纲作为主持人参加天津卫视《今夜有戏》节目录制。郭德纲的主持人生涯最早从安徽卫视邀请其担任两档节目的客串主持。2006年，郭德纲被安排与北京台当家花旦徐春妮一起，主持全新脱口秀综艺栏目《星夜故事秀》，为其大红大紫推波助澜。<br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://img3.cache.netease.com/photo/0001/2010-08-11/900x600_6DR28TO43R710001.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　2008年12月26日，郭德纲携徒弟何云伟、李箐在西安参加演出。2010年北京电视台《每日文娱播报》记者称，在追踪报道郭德纲别墅“圈地”事件时遭到郭德纲徒弟李国勇（艺名李鹤彪）殴打，郭德纲事后在相声中和博客表态力挺徒弟，直指北京电视台龌龊，言辞激烈。8月6日凌晨，李菁和郭德纲大徒弟何云伟突然宣布正式退出德云社。<br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://img3.cache.netease.com/photo/0001/2010-08-11/900x600_6DR29D7C3R710001.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　2010年8月7日，北京北纬路甲一号，离关门整顿就不到两天时间的的德云社前门庭冷落。7日凌晨，德云社在官方网站上挂出《关于停演内查的重要通知》。通知称，北京德云社特决定自8月9日起暂停所有小剧场的常规演出，开展深入自查，进行内部整改。<br />
