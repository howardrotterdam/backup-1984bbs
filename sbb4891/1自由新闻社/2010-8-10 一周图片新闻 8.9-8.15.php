													<h2>中国小体操运动员4岁开始艰苦训练</h2>
						
						
																											<div id="postmessage_780220" class="t_msgfont">　　<br />
　　英国《每日邮报》刊发了一组图片报道了中国小体操运动员艰苦训练的场景，这些小体操运动员从4岁就开始了严苛的训练。<br />
