长毛接受记者访问<br />
<br />
梁国雄议员（The Hon. Leung Kwok-hung，1956年3月27日－），绰号长毛，出生于香港，籍贯中国广东省增城，第三至四届（2004年─）香港特别行政区立法会议员，是左派民主人士，托洛斯基派的支持者，网络电台MyRadio节目足智多谋及萧鼓声中的主持。梁国雄是香港泛民主派的一员。2006年4月中，他和黄毓民、陈伟业等人组成了社会民主连线。<br />
<br />
梁国雄的长发及常穿的“捷古华拉”衬衫成为了他的招牌，故被称为长毛。长毛以及他所属的四五行动经常在香港各大（主要六四事件[1][2][3]）、小示威活动出现，亦曾经因“藐视立法会”而入狱[4]。<br />
