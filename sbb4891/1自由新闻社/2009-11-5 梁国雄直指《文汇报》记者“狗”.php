[编辑] 事迹<br />
<br />
长毛少年时在金文泰中学毕业。他自幼随母加入左派工会，中学时代参加毛派学生运动。毕业后他曾担任酒保、地盘工人、九巴洗车工人等基层职业的生活。<br />
<br />
1970年代，他加入当时的托派政治组织“革命马克斯主义联盟”（革马盟）。1979年4月，梁国雄曾因纪念四五天安门事件三周年及支持内地民主运动，被控以“非法集会”罪，入狱一个月。<br />
<br />
1988年，梁国雄结识一名德国女子，曾与她一同前往当地一个小镇过田园生活，最后分手收场。传媒指梁国雄曾结婚，但陶君行于2006年6月5日的《风波里的龙门阵》网上电台节目中指出梁国雄当时并无结婚，只是与该女子共同生活而已。<br />
<br />
1990年革马盟解散，他就与一群志同道合的朋友创办四五行动。<br />
<br />
1989年－1999年间，梁国雄认识了吴恭劭、龙纬汶、古思尧、雷玉莲等人，加上原革马盟及支联会的朋友，以及1991年被中共释放的刘山青等，令其示威生涯更添色彩。梁国雄以及其代表的四五行动广为人知，在于他们一次又一次在中共领导人访港期间抬假棺材至他们出席的地点示威。<br />
