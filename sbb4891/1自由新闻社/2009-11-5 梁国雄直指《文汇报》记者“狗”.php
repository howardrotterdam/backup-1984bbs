梁国雄出席2008年之七一大游行<br />
<br />
2008年立法会选举长毛梁国雄在新界东成功连任。但2008年9月7日梁国雄因闯入私人住处示威而被控非法集结罪，9月13日被判非法集结罪成立[5] ，9月30日被判社会服务令60小时。10月8日梁国雄与社民连两位议员黄毓民及陈伟业，被传媒合称“恶人铁三角”，顺利宣誓成为香港特别行政区第四届（2008-2012年）立法会议员。<br />
<br />
2008年11月8日，“长毛”梁国雄于为在囚人士争取狱中选举权官司中胜诉。<br />
<br />
2008年12月11日，梁国雄及陶君行去年在礼宾府东闸外焚烧报纸和车胎，及袭击一名警员及警长案宣判。案情指，奉中区分区指挥官李永光的命令，二名港岛警察机动部队姓李警员及姓关警长上前阻止，遭到梁国雄及陶君行袭击，李开动灭火器时，陶君行突然撞击他，梁国雄同时冲向李，令他跌倒。当关蹲下扶起李时，陶君行用双手拉他衫袖，将他推倒地上。两人早上在东区裁判法院被裁定三项袭警罪名成立，案件押后至2009年1月16日判刑；[6] 于判决当日获判社会服务令。<br />
[编辑] 立法会工作表现<br />
<br />
于2008年立法会选举，由香港电台所举行的论坛中，陈克勤指梁国雄经常缺席会议，并要争取惩罚缺席会议的议员。不过根据《2008立法会选举大会出席率》的报告[7]：<br />
<br />
&nbsp; &nbsp;1. 梁国雄的出席率达95%，与黄宜弘和黄容根功能组别议员相同，所以不是经常缺席。<br />
&nbsp; &nbsp;2. 如果计全体立法会议员（不计算患重病身故的马力），还有9名议员比梁国雄的出席率低。<br />
&nbsp; &nbsp;3. 就算只计直选立法会议员的出席率，也有3名直选议员比梁国雄的出席率低。<br />
&nbsp; &nbsp;4. 出席率最高有100%（全没缺席），一共有4位，分别是范徐丽泰、李永达、谭香文、张超雄，其次是99%, 98%, 97%, 96%。<br />
<br />
根据《2004－05年度特区立法会议员表现评估报告》指出，梁国雄是地方选区议员中“议案及法案的发言次数”（19次）及“发言次数”（53次）最多[8]。<br />
<br />
梁国雄亦是立法会保安事务委员会的委员。<br />
