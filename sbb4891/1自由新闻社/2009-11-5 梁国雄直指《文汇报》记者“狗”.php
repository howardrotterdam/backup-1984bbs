[编辑] 泛民主派<br />
<br />
和社民连其他成员不同，长毛和其他泛民关系一直不俗。例如于2008年立法会选举，长毛于香港电台选举论坛和民建联的陈克勤激烈辩论，两方就廿三条立法争持之际，黄成智更主动帮助长毛，指长毛有六万多票（04年选举），陈克勤不应指长毛硬闯议会。此外，郑家富名单的成员于长毛发言后，有时也跟随长毛支持者欢呼。<br />
<br />
另外，黄毓民于选举期间直指其他泛民主派“面目模糊”（虽然其后又表示要合作），又不断狙击公民党，但长毛于选举论坛并未攻击其他泛民，反而集中时间攻击民建联、自由党及庞爱兰三张建制派名单。当选后又公开对民建联的支持者说：“我地赢左五席，你加埋都未够我地零头”，明显视民主党、公民党及前Q为盟友。长毛并不常于泛民饭盒会出现，据说同陈方安生有关。事关长毛曾在饭盒会提出五区总辞，陈太反对并说考虑不再来饭盒会。<br />
[编辑] 与曾荫权关系<br />
<br />
梁国雄一向以激烈的方式示威著称，但他与曾荫权早在二十年前认识，不过因为他俩政治取向不同，所以近年已极少联络[9] 在2007年3月1日，行政长官候选人答问大会在湾仔会议展览中心举行期间，梁国雄在会场示威时不慎跌倒，曾荫权在翌日早上致电梁国雄致以慰问[10]。 在2008年12月，在立法会谈到包机事件时，梁国雄提到政府麻木不仁，公然喊特首仆街。惟当时并无受到在场官员及议员的警告和驱逐。大部分网民均认为梁国雄的表现英勇，为社会抗争。 在2009年6月4日，六四事件二十周年悼念晚会结束后，梁国雄在铜锣湾记利佐治街和大批参加晚会市民高呼“曾荫权仆街”的口号。<br />
