梁国雄在2000年的立法会选举曾经因为不满刘慧卿的政策过于保守，而与她在新界东选区对垒但高票落败。2003年，他又在区议会选举中越区前往北角锦屏区挑战蔡素玉，意图动摇福建帮在当地的铁票但落败。在2004年立法会选举中，长毛再度出选新界东。由于资金不足，所以并没有做大规模的宣传活动，连宣传板也要拾民主党及民建联丢弃的旧板，再在上面涂一层油来使用。最终他以六万多票（60,925票，接近稳夺一席所需之最低票数）顺利当选。商人胡应湘在选举前一度更曾声言若长毛当选，他会离开香港，但在选举结果公布后却收回有关言论。长毛知道自己当选后，表示会考虑宣誓效忠中国人民及香港市民，传统亲共左派势力则对他的当选不置可否。<br />
2007年8月香港扎铁工人的示威集会，梁国雄参与声援。<br />
<br />
2004年10月6日，梁国雄宣誓就任香港立法会议员，在正式誓词的前后，他加插了“效忠人民”及“平反六四”等字句。宣誓后他并无按惯例签署誓词。香港立法会秘书长及后表示誓词仍然有效。梁国雄就任香港立法会议员后，部分港人认为他只顾将其街头勇士形象带入立法会，未能以一个立法者身份去提倡并宣传自己的政纲及理想。不过，在2008年立法会选举中他稳夺取得新界东第二高之44,763票数，较第三高票的民主党黄成智名单多589票而成功连任。<br />
<br />
2005年4月29日，梁国雄等人在东区海底隧道乘车到九龙入口停下，并下车拉起横额抗议东隧加价，在2006年3月21日被判阻街罪成，判监12日，但获准以港币1,000元保释，等候上诉。在2007年1月5日，梁国雄等人在高等法院上诉得直，无罪释放。<br />
<br />
2007年6月3日，梁国雄联同捍卫基层住屋权益大联盟的成员到房屋及规划地政局局长孙明扬寓所外示威，期间梁国雄曾撞击大门。梁国雄因涉嫌参与非法集会于6月5日被捕；其助理区国权更加控袭警罪一项。<br />
<br />
2007年12月29日，梁国雄及陶君行全国人大常委会副秘书长乔晓阳访港到中区礼宾府演讲时抗议，在礼宾府东闸外焚烧报纸及车胎，期间两名警员上前希望扑灭火种时，遭梁国雄及陶君行阻止，两名警员被推撞跌倒在地上。期后被警方控三项袭警罪名。<br />
