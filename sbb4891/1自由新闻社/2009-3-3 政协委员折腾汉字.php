																											<div id="postmessage_208893" class="t_msgfont">新华时评：汉字“整形”需慎行<br />
<br />
　　据有关部门介绍，此次编制的《通用规范汉字表》历时8年，经过专家全盘考虑、反复研究才得以出台。但是，已经广为人们接受并广泛使用的常用字要不要改、怎么改，恐怕不能光考虑专家们所说的汉字“字理”问题，更重要的要看应用是否方便。 <br />
<br />
　　汉字是一种负载信息的书面符号。只要人们普遍愿意使用并能明白其表达的意思，这样的汉字就很好地完成了它的使命，无须修改；反之，那些容易让人产生歧义，或者书写不方便的汉字就需要修改。综观此次“整形”的44个汉字，绝大部分都具有通行度高、易于识别的特点，何必改头换面呢？ <br />
<br />
　　此次推行新字表的一个主要原因是为方便信息储存和管理，但是，一些字在“整形”后反而不规律、不统一，给应用徒添麻烦。例如“刹”和“铩”字中的“杀”字写法本来相同，在修改后写法却不一致了。这说明，此次修改在字的选择上并不成熟。 <br />
<br />
　　在50多年简体汉字的使用过程中，无论是新词的添加还是异体字的规范，每一次调整和改进都是建立在符合人们普遍的文字审美和使用习惯上。《通用规范汉字表》尚在征求意见阶段，44个汉字要不要“整形”，值得有关方面仔细斟酌。来源：天府早报<br />
