													<h2>台湾当局弃“汉语拼音” 美国人称像“井底之蛙”</h2>
						
						
																											<div id="postmessage_44675" class="t_msgfont">中广网北京11月14日消息&nbsp;&nbsp;日前，台湾“内政部”公布了《标准地名译写准则》草案，明文规定，年底前将把全部地名拼音改为台湾所谓的“通用拼音”。岛内舆论认为，当局此举分明是与国际脱轨，将使台湾更失竞争力<br />
<br />
&nbsp; &nbsp; 由于特殊的历史发展过程，台湾地区仍然沿用旧有的注音字母。平心而论，注音字母在注音识字教育过程中曾经发挥过积极的作用。但是，注音字母不是音素文字系统，只能作为汉字的注音符号，不能用做汉语的拼写体系，加上当初设计时的许多缺陷，注音符号已经不能适应台湾文化和经济、社会发展的实际需要，需采用一种新的注音或拼写系统。<br />
&nbsp; &nbsp; 祖国大陆的《汉语拼音方案》显然就是很好的选择对象，它充分考虑了汉语的特点，又尽量与国际接轨。1982年，国际标准化组织规定拼写汉语以汉语拼音为国际标准。当然，最主要的是，汉语拼音经过岁月的考验，已经较为圆满成熟。<br />
&nbsp; &nbsp; 但是，岛内偏偏有那么一些人，出于某种政治目的，不愿采用《汉语拼音方案》，一直沿用注音符号。后来，台湾当局炮制了一个所谓的《通用拼音方案》，并于2002年由台“教育部”宣布使用。这个通用拼音系统虽然只是对《汉语拼音方案》略加改动，但是却与汉语拼音大相径庭。同样一个人名，同样一个地名，汉语拼音和所谓的“通用拼音”可以完全不一样，更不用说其他差异了。比如，台北市的木栅地区，“木栅”的拼音，汉语拼音是Muzha，但是通用拼音则是Mujha，虽然只差一个字母，但是却相去甚远。再比如台北市区著名的“忠孝东路”的“忠孝”，汉语拼音为Zhongxiao，通用拼音则是Jhongsiao，差别更大。 <br />
&nbsp; &nbsp; 所以，“通用拼音”一面世，便受到岛内的批评和抵制。岛内舆论认为，“‘通用拼音’一点也不通用”，根本无法和国际接轨。台湾作家龙应台诘问，“如此一来，台湾将自外于世界文化。从政治封锁到文化封锁，台湾要用什么面对未来？”在台湾的外国人也一边倒赞成采用汉语拼音，反对另搞“通用拼音”。美国商会前主席韦杰夫以微软视窗依靠开放的相容性，在占有率上远超别家为例，说明“如果台湾2300万人坚持不用多少亿人使用的汉语拼音，实在太像井底之蛙”。 <br />
&nbsp; &nbsp; 面对各方质疑，时任“教育部长”的黄荣村只好在记者会上打马虎眼，说什么“通用拼音”不会强制执行，为了尊重各个族群，不会采取“一统天下”、“车同轨、书同文”那一套。话虽这么说，但在实际操作上，台当局规定采用“通用拼音”的县市，可以申请巨额补助，采用汉语拼音的则没有政策优惠。尽管这样，还是有一些眼光长远的县市选择了汉语拼音系统。比如台北市就在2003年正式敲定中文译音的原则，各个机关、学校、景点以及其他名称标示译音采用汉语拼音。格式以联合国标准为原则，采取名称连写、第一字母大写、其余字母小写的方式。至于一些约定俗成的特定词等，可以继续使用旧有译音。 <br />
&nbsp; &nbsp; 有关拼音系统的取舍，在岛内已经争议良久。政治外力的干扰，使得原本属于语言文化范畴的争议，俨然成为一场旷日持久的政治角力。此次台湾当局意欲彻底弃用汉语拼音，再次遭到岛内舆论的质疑。有识之士认为，台湾地区放弃大多数国家采用的汉语拼音，是自断台湾中文文化发展的后路。国亲两党也猛批民进党当局“政治压过专业、意识形态打倒实用”。岛内民众认为，台湾当局政治挂帅下牺牲的是学生的利益，因为通用拼音根本就出不了岛。有位台湾学生说，如果不会汉语拼音，对于我们这些学生的深造可是件麻烦事。这位学生说，我打算去俄罗斯留学，所以正在学俄文，我和很多朋友使用的都是中国大陆生产的俄汉电子辞典。但每当我用中文查俄文单词时就非常头疼，因为中文输入必须用汉语拼音，由于根本没有学过汉语拼音，因此每次都只能猜着输入。 <br />
&nbsp; &nbsp; 对于当局准备在年底前把全部地名统一采用“通用拼音”为基准，台北市长郝龙斌表示，为了要和国际接轨，台北市决定仍采用汉语拼音，因为汉语拼音是国际普遍使用的拼法。他表示，“内政部”如果要达致全岛统一，最好尊重一下专业，事前讨教岛内的语言专家。</div>

							
							
							
							
