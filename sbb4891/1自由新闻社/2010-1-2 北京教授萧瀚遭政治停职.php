																											<div id="postmessage_414962" class="t_msgfont">转过来――<br />
<br />
答汉风和雨君<br />
关于我的被不上课及其他<br />
萧瀚<br />
 <br />
汉风和雨君：<br />
您好！<br />
网上看到大作《萧瀚老师印象――有感于萧老师“被不上课”》，谢谢您对我的关心和期待。由于您谈到的许多问题多少有点关乎公共领域，所以，我想做一些回应是应该和必要的。<br />
一．被不上课是怎么回事<br />
其实，到现在我也不知道到底是什么原因，什么程序，什么理由，以及什么人作出这个停课决定。报道上说停课是“或因介入公共事务太深”，是记者问到这一点的时候，我说可能吧，并非我自己这么认为。<br />
我只知道下学期的选课单上没有我，本来至少有《中国宪政史》（本科部）或《经典著作导读》（研院）。至今院方也没有人跟我谈这件事情，可能他们太忙，或者不知道该怎么跟我谈吧，这些我都理解，唯一不理解的是停课本身。当然，这件事还没完，我先看看再说。<br />
二．关于我的写作<br />
我从小喜欢东看西看，对我来讲，只要是中文写的，只要感兴趣的，我都想了解。法学对我来说，是我认识这个社会的一扇窗户，是我剖析这个社会的一个工具，非我安身立命之所，我从未想过要做一个法学家，在中国这样的国度，我最想做的只是一个有尊严的人，连人都做不成，别的我以为大约都是没有根基的。正因为如此，我的写作是杂乱的，没什么体系，没什么专门的目标，一切围绕着关于人的思考，所以有专门的法学论文、时评文字，也有其他文体的文字，而且越随着年龄的增长，我越讨厌论文这种文体，除非真正有大思想，我不喜欢这东西，再说真有大思想，也可以有更好更合适的文体选择。许多时候，我甚至喜欢用格言体写作，您看着这些文字不以为然，我想那是个人偏好问题，我从来不会按照别人的要求和期待来生活，想必您也是如此。<br />
三．我是不是偏激、激烈了<br />
您以及其他一些同学，认为我偏激或者激烈，尤其近来更让你们觉得很难接受。我自认为是个择善固执的人，因此也会尽可能地从善如流，但在普遍被不能说话的语境下，没有足够的理由让我来接受你们的批评。如您所言，您和许多同学可能和我一样都讨厌或者反感某些现象，只是不喜欢包括我在内的许多人的反抗方式。这个问题，我没法和您讨论，除非咱们一起争取到了言论自由。此话头就此打住。<br />
至于我在文字中有时候会出现脏话，这是自然反应，比如唐福珍事件发生的时候，我诅咒了、谩骂了。您对此很反感，我理解，许多朋友都批评过我这样出口谩骂（我谩骂的已经很少了），有朋友甚至直率地痛斥我这是个人修养问题，我承认这是个人修养问题。不过，在一些特定情形下，对着或具体或抽象的他们，只是因为隔得太远，不然我会操家伙对他们――我倡导非暴力，可我也尊重正当防卫。<br />
您举例蔡元培不屑于戊戌人士骂人的例子，暗示我这样难当大任，熟悉我的朋友们都知道我从来对大任没兴趣，只对本应属于我的那份自由感兴趣，在公共领域发言，仅仅是因为那些公共事务与我这份自由荣损与俱的紧密关联。<br />
四．我能做什么和我该做什么<br />
我的童年是在饥饿与青山绿水中度过的，那是幸福的，但我也过早地开始看到不公与不义。青少年时代的偶像是刘宾雁先生和李敖先生（虽然现在毫无疑问删除了李敖，但依然感激他的启蒙），后来就没有偶像了，除了人之外，世上还有更高的敬畏，虽然一切人格高贵的人都是我充满敬意的。<br />
那么我能做什么，我所希望的自己，只是忠诚于我所认知的真理，想说话的时候尽可能不吞吞吐吐，同时对公共言论伦理负责。正如在课堂上，我不可能按照所谓大纲之类的东西来上课，因为这是上课，我得尽可能做到如陈寅恪先生所言：“别人讲过的我不讲，我以前讲过的不讲。”虽然无法做到，总得努力。这是作为一个教师，我目前唯一能做到的。<br />
除了我能做到的，我还应该做什么？<br />
我的本性是希望独善其身的，可是如秦晖先生所言“达则独善其身，穷则兼济天下”，当今时代，无人能独善其身，我被时代的洪流裹挟，没有能力对身边甚或社会之事完全超然物外，我的本性决定了做不到像蒙田那样“智者可以找到比驯服疯狗更有趣的消遣”（茨威格语），因为疯狗的尖牙利爪之下，是一个个跟我一样有喜怒哀乐七情六欲的大活人，他们的自由和生命与我连在一体，我虽没有心力和能力事事关心，但也没有能力事事冷漠。<br />
至于您提到的希望我能自我保护，我一直在自我保护，我的本性是个怯懦的人，恐惧一切可能加于我身心的枷锁，然而如晚明大儒孙其逢所言：“天下事只论有愧无愧，不论有祸。”我恐惧有祸，更恐惧有愧。<br />
每一代人都有每一代人的活法，每一代人也有每一代人需要去做完的事。当你们年轻的学子们对我的激烈不理解甚至不满的时候，我希望自己尽可能地理解你们的圆融，我很羡慕这样的圆融，但我做不到。<br />
虽然年过四十，但还能被人看成激烈甚至偏激，这是我的荣幸。虽然它不能证明我热圩杂桑更无法证明自由本身，但至少这是除了入狱之外，唯一还能证明我活着的证据。</div>

							
							
							
							
