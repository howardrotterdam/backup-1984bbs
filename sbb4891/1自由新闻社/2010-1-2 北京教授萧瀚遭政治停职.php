													<h2>北京教授萧瀚遭政治停职</h2>
						
						
																											<div id="postmessage_410182" class="t_msgfont">北京政法大学法学院副教授萧瀚被校方以“介入公共事务太深”为理由暂停教职曾被暂停教职一年的同校学者维权律师滕彪就认为，当今的中国知识份子介入公共事务太浅而不是太深(何山报导)<br />
萧瀚在被停职的翌日，在其微博客中挖苦自己，因为曾在课堂上说过，想当教育部长，当了部长就可以解散教育部。可能教育部怀恨在心了吧，学校无可奈何被作出决定。<br />
而萧瀚亦对自己的学生表达歉意。下学期的本科生、研究生都受到他被停职的影响。萧瀚认为，被停职对他的生活来说不算是坏事，但对他的自由却是大大的坏事。萧瀚说，“该是他自己选择不上课，而不是被不上课。”他并在新年，祝愿朋友们尽情地用笑声，“拉开专制崩溃的序幕。”<br />
记者尝试与萧瀚接触，萧瀚的好友则表示，当事人不希望接受访问。同是政法大学学者，维权律师滕彪就表示，当年因为签署08 charter，也被校方以停课一年。<br />
滕彪说: “这一年被学校停课，然后下学期安排了课，就是停科，不让上课，但是基本工资还可以发。”滕彪并说，萧瀚是一个好的老师。萧瀚也是有两三门课不让他上，是一个很好的老师，对学生也好，而且也很有学问。而且比较大胆敢言。对话式的，能够引发学生独立的思考，所以他的风格，包括他对中国一些独到的思考，会影响孩子。”<br />
08年初，政法大学教授杨帆与逃课的女学生发生肢体冲突，当时萧瀚对杨帆提出了批评，法大出现同校老师“互杠”局面，引起网络的一片热烈讨论。当时他曾提出过辞职。<br />
滕彪就说，以萧瀚“介入公共事务太深”就要停课，他是不认同，因为当今的中国学者，并没有对公众事务，负起足够的良心。滕彪说: “中国真正的知识份子，他实际上是关心这些公共事务，由其是一些国内的媒体封杀的，不能报导的公共事件，像萧瀚、贺卫方，他们都是非常优秀的代表。绝大多数的知识份子，他们是不会，不愿意参与，也不敢去在一些公共事件上发声音，只是搞自己的研究，多数是刨学术的垃圾。”<br />
据大陆媒体的报导，萧瀚这位大陆的前卫知识份子曾表示，“复习考试是天下最让人讨厌的事情”，而“政治课则是讨厌中之最讨厌”。</div>

							
							
							
							
