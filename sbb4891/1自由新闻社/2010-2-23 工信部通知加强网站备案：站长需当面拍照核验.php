													<h2>工信部通知加强网站备案：站长需当面拍照核验</h2>
						
						
																											<div id="postmessage_489261" class="t_msgfont"><a href="http://it.sohu.com/20100222/n270368544.shtml" target="_blank">http://it.sohu.com/20100222/n270368544.shtml</a><br />
【搜狐IT消息】2月22日晚间消息，据站长与IDC接入服务商向搜狐IT透露，福建省通信管理局与广东省通信管理局相继在网站发出进一步落实工业和信息化部关于网站备案信息真实性核验工作方案通知。<br />
&nbsp; &nbsp; 通知要求，基础电信企业和各接入服务商在向通信管理局提交网站申请备案之前，要对主办者身份信息当面核验、留存有效证件复印件，要对网站主体信息、联系方式和接入信息等进行审查。<br />
<img src="http://imgur.com/H7DPE.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
网站备案信息真实性核验、审核工作流程图<br />
<br />
&nbsp; &nbsp; 涉及到具体执行，网站负责人本人需要携带核验所需证件原件、材料到接入服务单位备案现场办理核验手续。接入服务单位需在备案现场采集并留存网站负责人彩色正面免冠照。备案中心统一制作、提供带有标识的幕布作为拍照背景，照片应显示拍照时间和背景标识。<br />
&nbsp; &nbsp; 目前，北京市通信管理局网站尚未下发上述通知。<br />
&nbsp; &nbsp; 以下为福建省通信管理局发布的《工业和信息化部关于进一步落实网站备案信息真实性核验工作方案（试行）》：<br />
　　为切实保证网站备案信息的真实性，落实《工业和信息化部关于进一步深入整治手机淫秽色情专项行动工作方案》中的要求：“基础电信企业和各接入服务商在向通信管理局提交网站申请备案之前，要对主办者身份信息当面核验、留存有效证件复印件，要对网站主体信息、联系方式和接入信息等进行审查”。依据《互联网信息服务管理办法》（国务院令第292号）、《非经营性互联网信息服务备案管理办法》(信息产业部令第33号)的相关规定，制定网站备案信息真实性核验工作方案。<br />
　　一、核验内容<br />
　　接入服务单位根据相关网站的委托代为履行备案、备案变更等手续时，应对网站主办者提交的主体信息、联系方式、网站信息，以及本单位提交的接入信息的真实性进行核验。<br />
　　1.网站主办者（主体）信息<br />
　　网站主办者是指互联网信息服务提供者，包括单位和个人两类。<br />
　　（1）网站主办者为单位，核验证件为：<br />
　　如网站主办者为机关、事业单位、社会团体，组织机构代码证书原件为核验其单位资质的第一证件，在没有组织机构代码证书的情况下，可核验事业法人证书或社团法人证书等原件。<br />
　　如网站主办者为企业，核验工商营业执照或组织机构代码证书等原件。<br />
　　如网站主办者为军队，核验军队代号证书原件。<br />
　　网站负责人的个人证件：如网站负责人为中国公民，身份证原件为核验其身份的第一证件，在没有身份证的情况下，可核验户口簿、军官证、台胞证等原件；如网站负责人非中国公民，则核验其护照原件。<br />
　　（2）网站主办者为个人，核验证件为：<br />
　　如网站主办者为中国公民，身份证原件为核验其身份的第一证件，在没有身份证的情况下，可核验户口簿、军官证、台胞证等原件。<br />
　　如网站主办者非中国公民，则核验其护照原件。<br />
　　（3）网站负责人的定义：若网站主办者为单位，网站负责人是指法定代表人或单位委派具体负责网站的部门负责人；若网站主办者为个人，网站负责人为其本人。<br />
　　2.联系方式<br />
　　如网站主办者为单位，联系方式是指网站负责人手机号码、办公电话、电子邮箱、通信地址；如网站主办者为个人，联系方式是指网站负责人手机号码、办公电话或住宅电话、电子邮箱、通信地址。<br />
　　3.网站信息<br />
　　网站名称、网站域名、涉及需前置审批或专项审批内容、网站服务内容/项目。<br />
　　4.接入信息<br />
　　接入服务提供者名称、接入方式、服务器放置地点、网站IP地址。<br />
　　二、核验、审核、核查规程<br />
　　（一）备案信息真实性责任主体<br />
　　接入服务单位根据相关网站的委托代为履行备案、备案变更等手续时，核验主体为接入服务单位，包括：基础电信企业省分公司（含市、县级分公司）、互联网接入服务单位（IDC、ISP）、公益性互联单位等。<br />
　　审核主体为各省、自治区、直辖市通信管理局。<br />
　　核查主体为工业和信息化部（委托国家互联网备案管理支撑中心，以下简称“备案中心”）。<br />
　　（二）备案信息真实性核验、审核、核查规程<br />
　　网站备案信息真实性核验、审核工作流程图见附件。<br />
　　1.接入服务单位网站备案核验规程<br />
　　（1）网站主办者登录接入服务单位备案系统录入网站备案信息。<br />
　　（2）接入服务单位受理网站主办者提交信息后，对主体信息、联系方式等信息进行预核验：初步判定主体信息是否真实，通过电话、邮件等途径核验联系方式是否正确。预核验后，接入服务单位应向网站主办者发送现场核验通知或信息退回的短信提示：“您提交的网站备案信息已通过预核验，请在1个月内由网站负责人本人携带有关证件原件到我单位备案现场办理核验手续”或“您提交的网站备案信息未通过预核验，请修改后重新提交”。预核验工作必须在主办者提交备案信息后5个工作日内完成。<br />
　　（3）网站主办者接到通知后，由网站负责人本人携带核验所需证件原件、材料到接入服务单位备案现场办理核验手续。<br />
　　（4）接入服务单位在本单位备案现场采集并留存网站负责人彩色正面免冠照（电子照片规格：800×600像素）。备案中心统一制作、提供带有标识的幕布作为拍照背景，照片应显示拍照时间和背景标识。<br />
　　（5）接入服务单位备案人员利用公安、质检、工商等相关部门提供的居民身份证、组织机构代码证、工商营业执照等信息源，核验网站主办者提供证件原件的真实性。同时通过网站负责人身份证原件核验身份证与当事人是否一致。核验无误，留存身份证明和单位有效证件复印件。<br />
　　接入服务单位备案人员登录本单位备案系统，依据证件原件内容对网站主办者网上提交的主体信息进行核验：核验证件持有者、证件类型、证件内容与备案系统中录入的网站备案信息是否完全一致，不一致不予受理；依据网站主办者提交的域名证书或域名注册机构网站公共查询信息，核验网站域名所有者与网站主办者身份的一致性，不一致不予受理；依据本单位对网站的实际接入情况，准确录入网站备案接入信息各项内容。<br />
　　（6）若核验无误，接入服务单位备案人员填写统一格式的《网站备案信息真实性核验单》（备案中心负责制订《网站备案信息真实性核验单》格式）；若发现备案信息有误，现场核实修改，并在《网站备案信息真实性核验单》中进行记录。《网站备案信息真实性核验单》一式两份：一份接入服务单位留存，一份上报网站主体所在地通信管理局，加盖接入服务单位公章。网站主办者和接入服务单位同时签订信息安全管理协议书。<br />
　　（7）在确认备案信息全部核验无误后，接入服务单位通过本单位备案系统向主体所在地通信管理局备案系统提交网站备案信息，并提交《网站备案信息真实性核验单》纸制原件、传真件或电子扫描件。<br />
　　2.省通信管理局审核规程<br />
　　各省、自治区、直辖市通信管理局登录省局备案系统，在二十个工作日内对接入服务单位提交备案信息进行审核。审核合格下发网站备案号，接入服务单位实施网站接入；不合格将备案信息退回接入服务单位系统，由接入服务单位重新核验。<br />
　　3.备案中心核查规程<br />
　　（1）备案中心受工业和信息化部委托，建设网站备案信息校验平台，对各通信管理局提交网站备案信息中的身份证信息进行全量自动核查。结合人工电话抽查，每月对备案系统提交的网站备案信息进行准确性核查和抽样评估并反馈情况。<br />
　　（2）备案中心每季度对部备案系统中存量网站备案信息准确率、备案率分省分接入商进行核查和抽样评估并反馈情况。<br />
　　三、工作要求<br />
　　（一）接入服务单位核验工作要求<br />
　　1.组织保障和制度完善要求<br />
　　各接入服务单位应在2010年2月底前设立现场核验网站备案信息部门，专门负责网站备案信息真实性核验工作，并实行单位领导负责制。应建立本单位的备案业务规范，备案工作考核制度。明确备案人员的工作责任，对备案人员业务能力实行年度考核，将日常备案工作质量作为考核的重要指标。<br />
　　各接入服务单位应在2010年3月底前正式实施网站备案信息当面核验，并将工作开展情况报许可证发证机关和单位注册所在地通信管理局。<br />
　　基础电信企业应在2010年4月份对租用本单位电信资源从事接入业务的接入服务单位是否设立当面核验人员、履行当面核验备案信息的情况进行检查，对未履行当面核验责任的，不得为其提供电信资源。<br />
　　2.业务明示要求<br />
　　接入服务单位在业务经营中应向网站主办者明示开办网站要依法办理备案手续、网站备案流程、需提供的证件、材料清单及网站备案有关注意事项。<br />
　　3.业务合同内容要求<br />
　　接入服务单位应在与网站主办者签订业务合同中，明文要求：“依据《非经营性互联网备案管理办法》第二十三条规定，如备案信息不真实，将关闭网站并注销备案。请您承诺并确认：您提交的所有备案信息真实有效，当您的备案信息发生变化时请及时到备案系统中提交更新信息，如因未及时更新而导致备案信息不准确，我公司有权依法对接入网站进行关闭处理。”双方签字、单位盖章确认。<br />
　　4.信息安全管理责任要求<br />
　　接入服务单位建立网站主办者资料档案，妥善保管核验过程中获取的网站主办者证件信息、照片信息、《网站备案信息真实性核验单》、与网站主办者签署的各项协议，保证信息不泄露，承担对网站主办者提交材料的信息安全保密管理责任。上述资料至少留存5年以上，以备通信行业主管部门依法进行检查。<br />
　　5.网站备案信息更新要求<br />
　　接入服务单位应做好日常回访核查工作，确保网站备案信息变更后，网站主办者及时更新主体信息、联系方式、网站信息。同时接入服务单位应根据网站接入变化情况，及时更新接入信息，并配合主管部门做好网站备案信息真实性核查工作。因未开展日常回访核查工作导致接入网站备案信息不准确的接入服务单位，主管部门依法对其进行处罚。<br />
　　6.存量网站备案信息核验要求<br />
　　对备案信息真实性当面核验工作正式启动前接入的网站，各接入服务单位要在2010年2月底前制定备案信息真实性核验工作详细计划，报许可证发证机关和单位注册所在地通信管理局，并在2010年9月底前完成全部网站的备案信息真实性核验。<br />
　　（二）省通信管理局审核工作要求<br />
　　1.做好日常审核工作<br />
　　各通信管理局应做好日常网站备案信息真实性审核工作，保证对重点信息（营业执照、身份证等）的逐一细致审核。二期备案系统升级改造后可由各通信管理局审核人员用口令登录接入服务单位系统抽检审核证件原件，同时检查接入服务单位上交的《网站备案信息真实性核验单》。<br />
　　2.检查“当面核验”工作开展情况<br />
　　各通信管理局对接入服务单位当面核验网站备案信息的部门设立和工作制度完善情况进行指导和检查。根据接入服务单位保存的真实性核验证明材料，对接入服务单位“当面核验”工作的开展情况、接入服务单位提交备案信息的准确性进行定期检查或不定期抽查，对发现未执行“当面核验”或提供虚假信息的，应严肃处理。同时将日常检查考核结果作为许可证年检重要参考。<br />
　　（三）备案中心核查工作要求<br />
　　1.新增网站备案信息核查<br />
　　备案中心每月对新提交的网站备案信息进行准确性抽查。对备案主体信息中身份证、组织机构代码证等信息进行重点抽查核查。<br />
　　2.存量网站备案信息核查<br />
　　备案中心按季度对系统存量网站备案信息进行准确性核查，对各接入服务单位真实性核验网站备案信息工作开展情况、完成进度、工作质量进行核查和抽查评估。<br />
　　3.制定核查标准和监督评估办法<br />
　　备案中心根据工业和信息化部的委托，适时制订、细化、完善网站备案信息核查标准和监督评估办法，并向各通信管理局、接入服务单位发布。<br />
　　4.加强对工作开展情况的督导、统计<br />
　　备案中心根据工业和信息化部的委托，对各单位落实网站备案信息真实性核验工作开展情况进行指导帮助和监督评估。加强对各单位落实网站备案信息真实性核验工作情况的分类统计，开展对各地通信管理局行政处罚情况汇总等工作。<br />
　　5.开展网站备案业务培训<br />
　　备案中心根据工业和信息化部的委托，编写、制订网站备案培训资料，组织开展面向各接入服务单位的全国性网站备案培训工作，对应掌握的网站备案相关政策法规、业务知识进行统一讲解。<br />
　　四、问责<br />
　　各省、自治区、直辖市通信管理局根据属地化管理原则，对未按《工业和信息化部关于进一步深入整治手机淫秽色情专项行动工作方案》中提出的“对网站主办者身份信息当面核验、留存有效证件复印件”要求开展工作，未认真开展网站备案信息真实性核验工作，提交不真实网站备案信息的接入服务单位，依据《非经营性互联网信息服务备案管理办法》第十条和第二十四条的规定依法处罚，责令限期改正。</div>

							
							
							
							
