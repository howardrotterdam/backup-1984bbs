　　还记得温州05年集资建房把,到现在都还没有入住.地方&quot;我们公司&quot;一开始要求必须要由防地产商牵头开发,集资方妥协了.后来开发商居然作出了一个每平米 7000元建筑成本的方案!于是又折腾了一番.最新的消息是,地方&quot;我们公司&quot;要求必须走商品房的销售流程.现在周遍的房价超过2万了,走商品房销售意味着巨额的各种税费.于是就这样,5年了,楞是没住上.<br />
