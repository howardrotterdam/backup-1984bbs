													<h2>传新房产税将于国庆前后出台</h2>
						
						
																											<div id="postmessage_839495" class="t_msgfont">传中共将出台房产税<br />
<br />
2010年09月21日 01:38第一财经日报【大 中 小】 【打印】 共有评论26条刘宝强 叶开<br />
<br />
房产税政策迟迟不出台，成为悬在楼市上方的达摩克利斯之剑。<br />
<br />
“目前有几个方案,但是不知道具体用哪个，但是估计明年元旦实施的可能性比较大。”昨日，有消息人士对《第一财经日报》记者透露，新的房产税是对原来的房产税政策进行修改，可能在国庆前后出台。<br />
<br />
不过，开发商似乎并不知晓此事。SOHO中国[5.36 -0.74%]董事长潘石屹昨日对本报记者表示，自己未听说即将出台房产税方案的消息。<br />
<br />
1986年开始实施的《中华人民共和国房产税暂行条例》目前只针对城镇经营性房屋和出租房屋征税，对个人所有非营业用的房产则免征房产税。由于开征物业税程序复杂，专家普遍认为，扩大房产税征税适用范围是比较可行的做法。<br />
<br />
上述消息人士也表示，这次有关部门重新修改房产税，“加上新东西，不需要人大批准，国务院批准即可，时间短,效率高。”<br />
<br />
早在2003年，北京、辽宁、江苏、深圳、宁夏、重庆等六省市就开始进行物业税“空转”。2007年，税务总局和财政部又批准安徽、河南、福建、天津四省市为房地产模拟评税试点地区。今年以来，关于上海和重庆将获准开征物业税或房产税的消息满天飞。日前又有消息称，针对房产征税的试点地区可能定在京、沪、深、渝四个城市。然而至今没有开征的时间表。<br />
<br />
昨天，一位熟悉发改委系统的机构人士向本报记者透露，上海的房产税试点方案上次在上报国家发改委被退回后，目前还在修改中，尚未上报。<br />
<br />
与以往调控多重视短期效应有所不同，本轮调控更加重视房地产市场长效机制的建立，这种背景下，作为长效机制之一的房产税或者物业税更令业界期待。<br />
<br />
近期，包括新华社、《人民日报》也多次刊发报道或者发表评论，探讨调控政策再度收紧的可能性。<br />
<br />
《人民日报》近日的报道称：“就目前情况来看，楼市调整与反弹可能并存。政府部门应及时摸清市场情况，公开有关数据和分析结果，并尽快告诉公众下一阶段调控的走向，以稳定市场预期，进而稳定供求关系、稳定市场。”<br />
<br />
我爱我家副总裁胡景晖对记者表示，就当前市场状况而言，政府可能对调控的效果并不是很满意，下一步政府动向可能有两方面，一是现有政策的严格执行，要加大地方上的落实力度，另外一个就是出台几个长效政策。<br />
<br />
“长效政策中，我觉得最有可能尽快推出的就是房产税。其他像土地制度改革要涉及立法问题，以及政府土地财政依赖等，近期推出的难度较大。”胡景晖说。<br />
<br />
链家地产副总裁林倩则并不认可把房产税作为调控房价的一种短期手段，而是应该视之为一项长期制度。“调控楼市就出台房产税，而扶持楼市时就再度减免房产税，如果这样频繁地调整政策反而会扰乱市场预期。”<br />
<br />
不过，出台房产税政策显然不仅仅在于短期调控房价，还有更为深层的原因。<br />
<br />
中国人民大学财经金融学院教授安体富就在6月份曾表示，房产税一旦开征，其规模将达到7000亿元。不小的房产税额度将会给地方税收以很大的补充，同时一旦开征房产税必将对地方依靠出让土地的财政模式给以彻底的改革。<br />
<br />
而终结地方政府的土地财政在不少专家和业内人士眼中，是抑制楼市过热釜底抽薪的举措。</div>

							
							
							
							
