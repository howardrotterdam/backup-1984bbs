　　借鉴国外房地产市场化制度，杜绝炒房，还住房以居住功能，兼驳任志强之流的无耻<br />
　　从任志强之流的言论中可以看出，房地产以及相关的利益代言人，如此高调的为本行业利益摇旗呐喊，而置公众的权益于不顾，已经到了多么无耻和强词夺理的地步。简直是睁着眼睛说瞎话。再看中房协，最近又向&quot;我们单位&quot;递交了暂缓房地产调控的申请。09年初他们这么做了一回，成功的主宰了国家政策，后果是什么，大家看到了，房价暴涨，社会激荡，差点毁掉改革开放以来的全盘好局。现在，调控才刚刚开始，房地产商连根汗毛还没掉，他们就又来了。<br />
　　这是一个可悲的时代，可悲在发出声音和把持话语权的都是些什么样的人？代表广大民众实际利益的声音太弱太无力。我大老张虽然不是名人，但是我见任志强之类出来晃荡一次就想痛骂一回，这是些什么东西？<br />
　　之所以之前有人说听了任志强早买房赚钱，就是因为中国的房地产制度是在学香港模式，用高价拍地，鼓励炒房的这套制度把房价炒起来。关于香港模式和地产财富的危害，简单的说，在香港这样一个经济和社会保障水平相对较高的城市这么搞，也许可以接受；如果全中国都这么搞，在最初几年的财富和GDP猛增后，由于财富迅速被透支并被集中到极少数人手中，会迅速进入一个社会震荡矛盾激化的时期，资源分配和社会结构将朝着极不稳定的方向进行，人心尽失，而人口构成将变动剧烈，大大加速老龄化，中华民族整体上将以历史上前所未有的速度衰亡。我在收集资料，将写长文阐述地产经济模式的危害。这里就不多说了。总之，这条发展道路是要被后人批判的。而只要稍微借鉴国外成熟制度，可以保证中国的房价基本不可能有多大的涨幅，更不可能暴涨，炒房的几乎会绝迹。全社会都会回到诚实劳动合法经营，发挥个人创造力的良好发展道路上。这些和急切的当前&quot;我们公司&quot;利益以及GDP政绩相悖，所以在过去没有被采用，但是不表示永远不会采用。 <br />
　　至于现在，可千万别听形形色色的房地产代言人忽悠了，房价真的到顶了。甚至可以说无论&quot;我们公司&quot;出台什么政策，态度如何，至少几个一线城市的房价绝对没有多少上涨的空间了。见我博客的资料，北京与世界公认的昂贵的东京房价的对比，北京中心区域的房价已经赶上甚至超过东京了。而北京与纽约的房价对比还要夸张，北京的普通住宅的价格已经是纽约独立house的2-3倍。而城市收入差距还有5-10倍。具体数据我就不多说了。相比起当前不动产的价值，银行的可发放贷款都显得太少，以至于人民银行不得不直接控制贷款流向，否则银行体系真的有崩溃的危险。因此现在除了禁止三套房贷，还新出台了收回贷款审批权以及资本金比率之类的规定。这是必须的。可想而知，在当前价位下，如果引入国外的成熟房地产制度，稍微控制一下炒房，房价向下的空间有多大。而这一天早晚要来，因为&quot;我们公司&quot;必须开拓新的财源来弥补收入亏空。<br />
　　对于个人来说，现在进入房地产，高位接手，有把自己一辈子搭上的危险。房价涨的时候杠杆效应赚钱爽的不得了，一旦房价不涨甚至下跌，正常收入绝对无法填满房价的窟窿。即使是较早开始炒房，现在账面收益很好的那批人，按照现在的成交量，什么时候能退出来清算收益都不得而知。毕竟，现在房产的总价格已经远远高于实际流通的货币总量。当然，这些人很有可能给下一届&quot;我们公司&quot;的财政收支平衡作出很大贡献。而房地产商，别看每个楼盘都赚了超额利润，最后一块高价地不能出手就会清零。3年后一看，能全身而退的房地产商不知道有几个。任志强所在的这种资本金薄弱的小地产公司，在一个发展成熟的地产行业里生存的机会微乎其微。另外要提醒的是，房市周期很长，比股市长的多，失手一次可能毁掉一生，甚至直接走上不归路。我知道有一个北大的校友，曾经买下过70多套房产，仅仅因为房产在上次调控中小小的震荡了一下，在08年12月份跳楼身亡。这是多么可悲的事情。所以我绝对不炒房。从现在开始，我们这批人都不应该炒房。否则真的要猪头了。 <br />
