																											<div id="postmessage_853000" class="t_msgfont">中国的房地产价格一定会降(转载) <br />
作者：挪威__森林&nbsp;&nbsp;发表日期：2010-10-2 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; <br />
　　中国的房地产价格一定会降<br />
　　　　　　<br />
　　首先， 来看看理智的空方认为房价要大跌的理由：<br />
　　1． 房价已经远远超过居民的收入承受能力<br />
　　2． 租售比不正常<br />
　　3． 价格比发达国家都高<br />
　　　　　　<br />
　　其次， 再来看看理智的空方认为&quot;我们公司&quot;要调控的原因：<br />
　　1． 担心由于房价引发社会矛盾，对政局造成影响<br />
　　2． 担心过多的泡沫导致崩盘后引发的社会经济问题<br />
　　　　　　<br />
我这里没有提及所谓的解决LBX的住房民生问题，因为我相信理智的空头都不会认为这是调控的原因。<br />
因此从长远看楼市一定会跌，如果这次&quot;我们公司&quot;调控的有水平那就是房价下跌，软着陆；<br />
　　如果这次调控失败那房价一定是大涨的，但最终结果还是房价下跌，不过是硬着陆的崩盘；<br />
　　当然就目前而言&quot;我们公司&quot;的手段似乎不怎么高明。<br />
　　补充一点，我不反对利益最大化，谁都想尽可能多的赚钱，但一定要有度；说鱼肉百姓，这也是学问，像捕鱼，你不计生态平衡的大肆捕捞刚开始看起是不错,大有上涨之势，但捕捞个几年呢？最后是鱼也没了，自己也饿死了；聪明点的捕一些养一些，这样才叫长久，现在的情况是中国的房地产捞过界了，激起民怨了。 <br />
　　美国经济学家库兹涅兹认为经济中存在长度为20年左右的长期周期。这种波动在许多经济活动中，尤其是房地产业中表现的特别明显，所以库兹涅茨周期也称为房地产周期。<br />
　　房地产的周期波动简单而言分为四个阶段：复苏、繁荣、危机、萧条。市场经历一段低迷期（萧条），成交量开始逐步回升，房地产价格随之逐步回升，房地产投资效应也随之显现；随后交易量日益放大，拉动房地产价格迅速上升（繁荣）；当价格过高时，受购买力制约，成交量开始下降，但房价仍保持上涨；随后受成交量下降影响，高房价难以持续，开始下降，进入量价齐跌的萧条期。　　<br />
　　香港房地产85年开始复苏到1997年顶点用了12年，到2003年再次复苏，周期为18年。中国房地产98年开始复苏到2010年恰好也是12年，中国房地产93年开始衰退到2010年是17年。<br />
　　美国房地产业从1989年开始衰退，到2006年再次衰退，周期为17年。<br />
　　所以现在就是十几年内的房价大顶了。考虑到人口结构的影响，中国十几年后的房子是过剩的。现在很可能是历史最高顶了。在2011－2012年下降的第一阶段，房价将迅速下降，较高峰下跌30％－50％，然后缓慢下降。 <br />
　　房价会降的超乎想象，房地产投机不可持续<br />
　　每天在北京，总被各种刚需的论调包围着，慢慢就麻痹了，接受了。<br />
　　走了几个城市以后才发现，完全不是一回事，从全国范围来讲，已有住宅面积大于实际需要的（所谓实际需要不是每个人都有房，任何社会也是让相当多一部分居民有房，房子都应该是年轻人通过一段时间的奋斗得来），只是因为房子都被当做投资投机来做，空置率非常之高。在很多城市，现在的城市建设基本都是住宅，大片大片的小区，把城市围着。房地产市场绝对是不可持续的，就看&quot;我们公司&quot;的态度如何，只要收紧银贷，没有任何不降的道理。查了相关资料，如果按照亚洲其他国家如日本的标准，中国城市的住宅早就过剩了，把全国人民搬进城里都有余。现在的问题是分配不均和住宅质量，而不是总量了。<br />
　　好了，回到北京，很多人说了，北京人多，刚需旺盛。好，我承认北京的刚需比之其他城市更胜一些，但是投机也更重了，我们知道，不可能每个人都能在年经的时候就有房，所有的刚需打个六折，现在北京的空置房子也足以应付这部分需求了，况且，只要全国一处房价出现松动，马上将如多米诺骨牌一样向周边传染，以投机资金的敏感和追求资本最大化以及趋向避险的本质，必然呈现资金的流出。现在这种情况，高价房已经卖不动了，4万以上一平的新房成交量停滞，而二手房就更难了。那些还持有三四环内动辄500万，千万一套豪宅的炒房客，理论上的财富可能要在账面上停留很久。如果还有贷款要还，资金链断裂的危险不是开玩笑的，中国是没有个人破产的，欠银行的债是死了也要还的。因此新的投机资金全部缩回去了，不可能接手这样的盘。而刚需买不起，愿意自住城里的有钱人还真不多。基本上，北京的有钱人集中在东北边的别墅区和郊外。那里的环境舒服的多。 <br />
　　资金流出房市，去向哪里？放眼望去，实业过剩，而习惯了不劳而获的资金也已经不大可能会赚实业的辛苦钱。那就只有一个去处，股市，从股市牛熊的周期来讲，也到了恰当的时间点。（注：我不是推荐股市，股市实际还是大鳄的游戏，据此投资，风险自负！）。除此之外，一切价值浓缩利于保存来源垄断的东西，都是这些资金的去处。什么东西？珠宝玉石，古董艺术品，呵呵。<br />
　　北京的房价回到09年初甚至更低几乎是必然的事情。即使是09年初的价位，新一代的自住需求买起来也很勉强。只要投资客不买，或者没办法用已有的房子抵押来买房，要靠自己的辛苦钱来买房，09年初的价位也是高不可攀。但是不排除有些波动。允许某些人对房市持乐观态度！甚至还可以希望房价再涨点，涨快点，因为不破不立，欲其生，先置于死地。别忘了07年股市，5-30之后还有一波大的呢，然后就稀里哗啦6000点下到1600了。虽然房市股市不能直接类比，但是也多少反映了投机市场的共同特点。 <br />
　　最近，除了第三套房停贷外，银行还做了房价下跌60％的压力测试。这些很多人都知道了。其实还有一些实质性的政策，不为大部分人所知。房地产开发商以及相关代言人都不敢提到一个字。因为他们真的害怕了。看到任志强最近频频发文了吗？简直气急败坏，丧心病狂。这是有原因的。<br />
　　这些政策包括：<br />
　　房地产开发贷款权上收北京；已经审批的，还未发放的立即停止；已经投放一部分的，后续发放的贷款也要上报北京。<br />
　　这和08年上半年是多么相似，从中我们可以了解压制房地产的进程，仍然在处于持续严厉之中，08年上半年这种搞法出来之后，导致了下半年房价显著下跌。<br />
　　需要指出的是，这一波调控政策比2008更加严厉。3套房停贷前所未有。银监会还发文件规定，信托公司的融资类业务额占银信理财合作业务额的比例不得高于 30%，比例超标的公司业务将暂停，直至达到规定比例要求。此外，文件还规定，投资类银信理财产品不得投资非上市公司股权。据悉，目前银信合作理财产品突破2万亿元人民币，而去年9月不足6000亿元。银监会把表外资产纳入表内，要求银行计提资本。银行的资本金有可能需要上千亿元的规模，刚刚在证券市场融完资喘气歇息、等待银信合作理财产品利润的银行不得不再次面对资金压力。如果此说成真，那么，严厉的监管相当于变相提升了0.1%以上的准备金率。<br />
　　这一波调控，由于资金链收紧的速度加快，房价会比08年跌的更快。从07年9月发政策到09年2月开始回暖，共16个月，跌到底。这次估计要减半8个月到底。从10年4月政策出台，8月份政策强化实施，再加8个月，到11年3月到底。而现在房价处于高位，比起08年涨的太多。到底跌多少，难以预料。<br />
　　有一点是肯定的，就是这次调控会有一个比较长的时效，短期内不会再出台类似09年初的救市政策。主要原因在于，当前国内国际的经济形势已经不允许继续吹大房地产泡沫。而国有银行的存贷比，资本金率，在近一两年内也不足以支撑起下一轮的地产泡沫。再像09年这么搞银行真的要跨，人民币体系都要危险。这种情况&quot;我们单位&quot;&quot;我们公司&quot;是绝对不允许发生的。而2012年就要换届，即使下届&quot;我们公司&quot;继续把房地产业当作支柱产业，也不会在任期开始的时候放开地产调控。那样，后面的经济政策回旋余地太小，没法持续玩下去。因此，至少三年内不会再出台救房市的政策。必然的结论是，相当一部分房地产公司会撑不过接下来的三年，尤其是那些资本金薄弱的中小地产公司。必定会有一大批地产公司因为资金链断裂而破产。地产盈利，也会回到相应的合理水平。我预言A股一定会出现几毛钱的地产股。这一天不会太遥远了。&quot;我们单位&quot;&quot;我们公司&quot;别无选择，不摧毁相当一部分开发商，就会毁灭中国经济以及社会稳定的大局。<br />
　　现在要做的，静观棋变，等待房地产冷却之后的救市行为出现，根据力度，再来评估是否进场。而去年底以来高位接手的炒房客，很可能面临跳楼的命运。即使那些进场早的炒房客，据我了解，基本上还是继续持有，特别是高价值的所谓好地段房子，跑掉的很少。虽然帐面收益不少，但是在变现落袋为安之前，仍然停留在帐面。这一波创造的财富大多是虚拟的数字泡沫。全世界的不动产，现在的市值约有480万亿美元。而泡沫最严重的，在东亚和中国。而世界上总共有多少货币在流通？不及这个数字的1／10。广义美元存量不过16万亿，美元M2才8.8万亿。无论发行多少货币，实行多么宽松的货币政策，都不可能有这么多的真金白银来兑现这笔纸上财富。最终这笔财富的至少一半会消失。这就是为什么资产泡沫必破的原因。<br />
　　日本在战后的1947-1949的三年间，出现历史上的生育高峰，扣除当年死亡人口，平均每年增加200多万的人口，当这批人婴儿潮时期出生的人逐渐成年后，日本迎来劳动力充沛,生产力迅猛发展的经济奇迹。在这些人处于壮年（30-40岁）时期，正是日本国力鼎盛的70 年代中期到80年代末这一段时间，那时日本由于有充沛的年轻劳动支撑，日本牛气冲天，生产力和购买力都非常旺盛，日本的GDP在世界日益逼近美国，引起美国和世界恐慌，反映日本股市冷暖的日经指数飙升到三万九千多点，房价也高到令世界乍舌！1990年前后，日本股市和房市相继进入衰退，股市暴跌70%以上，日经指数直到现在也只在九千多点挣扎，不到高峰时的四分之一！房价更是一泻二十年，套牢无数房奴。这不是偶然的，日本40年代末50年代初出生了很多人，这些人在婴幼儿时代可能成为日本的人口负担，但当这些人进入成年后，为日本提供了充沛的劳动力和消费市场，拉高日本股市和房市，使之纷纷创出天量天价！随着房价日益升高，生活成本也日益抬升，生活和工作的压力使得日本人的生育愿意一再降低，现在每个日本育龄妇女平均生育的孩子不超过1.2个。当前，日本每年出生100万左右的婴儿，但却死亡100多万老人，日本人口已经连续多年负增长！消费和住房失去了人口支撑，只有一跌再跌，日本的衰退期还很长，但日元会非常坚挺，因为日本也是老人政治，掌权的日本老人不希望自己年轻时代挣的钱贬值。纵观日本这段历史，日本的生育高峰在1949年前后，日本股市和房价的高点在1990年前后，人口出生率高峰与股市房价高峰相隔40年！ <br />
　　中国从1950年代直到1991年，除了大跃进的1958-1960的三年困难时期，由于饥饿导致妇女天然节育很少有小孩出生外，正常年份,中国每年出生人口都在2000万左右或者以上，其中1963年到1974年这12年都达到平均每年出生2600万以上，1970年是中国历年最多，一年出生了 2800万人口。当六七十年代中国婴儿潮出生的人进入成年后，中国也自然地迎来了劳动力异常充沛的时代。加上中国的生产模式主要是土建和加工贸易，需要大量的初级劳动力，这些人刚好派上用场，因此中国迎来生产发展的高峰，经济持续增长。有生就有死，目前死亡人口主要还是1930-1940年代出生的，这部分人口不多，当时每年也就出生七八百万人，据国家统计局统计，2006年、2007年中国死亡人口分别为892万、913万，扣除非正常死亡的人数，基本上与上世纪30年代每年的出生人口吻合！根据同样的道理，10多年后，当建国后出生的人转入衰老和死亡阶段，中国每年死亡人口将是现在的两三倍，达到每年 2000万左右，届时中国的70后出生的人已经进入中老年，90年代出生的人正好进入生育年龄，由于这批人当年每年只出生了1600万左右，加上由于性别歧视，每年又只有700万左右为女婴，就算90后女人个个都愿意正常生育，按照目前每个育龄平均妇女生育1.2个孩子计算，一年也就出生800万左右的婴儿，出生减去死亡，届时，中国人口每年可能净减少千万以上！由于中国前面长期实行计划生育政策，导致人口结构成了421模式。在90后成为劳动力主力的时候，社会上将到处都是老年人，劳动力也将短缺，住房也将严重过剩。届时股市不会涨，房子也会阴跌。可怕的是，以GDP为纲的中国，那时人均仍然没有发展到发达国家水平。如果国家财政从现在起不直接加大养老保障体系的投入，大量的税收财富继续花费在行政支出，维稳费，甚至直接填补银行因为滥发地产开发贷款造成的亏空的话，现在这种年轻人交钱养老年人的社会保障和养老体系必定会破产。<br />
　　中国的股市见顶在2007年，2010年房市也很可能见顶，因为中国人口出生高峰在1970年，到2010年这些人已经40岁，正是从壮年开始走下坡路的年龄！这也与日本走过的路相同。<br />
　　我总结一下：当年人口负担（多出生的人），成为现在的人口红利，现在的人口红利（一胎化），会成为将来的人口负债。<br />
　　我将各个年龄阶段的人拍排序，分析一下他们现在和将来可能的生活。<br />
　　1.现在的老人（1930-1950年出生的人），他们是幼年处于&quot;过家家&quot;，中年贫苦，老年幸福；<br />
　　2.现在的中年人（1955-1970年出生的人），他们是少年穷苦，中年辛劳但是较为稳定（经历文革，改革和社会变动，但是因为填补文革真空，职业发展较为顺利）， 老年平淡（届时到处都是老人，社会供养负担很重，而房子绝对会成为负担而不是财富）；<br />
　　3.现在的青少年（80-90前后出生的人），他们是少年幸福，中年辛苦（房子，还有一对年轻人头顶上有四至六个老人），老年凄惨（他们中年由于税收重，没有积累。而那时候房子是沉重的负担，持有税会高到一退休就住不起的地步，绝对不是财富，不可能像现在这样维持多数老年人有体面的生活）。 <br />
　　附录是中国从1929年到2008年每年的出生人口数量。请注意当前中国的出生率和日本是一样的水平。中国之所以人口没有负增长，仅仅是因为建国前出生的人少，死亡的老人数量和人口总量不匹配。但是2000年以后很多年了，出生小孩的比例和日本已经是一样的了。而人口出生率不改变的话，在接下来的10多年内，中国将出现每年千万级别的人口减少。<br />
　　结论与建议：1.中国的房地产再怎么忽悠，也已经达到人口结构匹配的巅峰了。按照日本的人均居住面积，中国城市已经建成的房子足够把全国人民都搬到城里。考虑到现有的房产大量空置，相当一部分集中在少数人手里，而房价收入比比日本更甚若干倍，房地产泡沫的危害将更甚，这可能会引发社会大规模动荡。国家应当严厉打击房地产炒作和空置率，虽然已经是亡羊补牢；2.中国将迅速进入老年社会，未富而先老；3.现在的年轻人忙忙碌碌，争做房奴,但他们普遍晚景凄凉！而上交的养老金中个人帐户比例低的可怜，还有专家随时惦记着要进一步减少; 4. 国家必须立法保障个人养老帐户的资金安全，并用财税收入以及国有企业的盈利来补充社会保障基金。在年轻人比例最高的时候国家必须攒钱养老。否则，中国人都不会觉得有保障和安全感，国内消费永远拉动不起来。5. 建立覆盖全国人民的社会医疗养老保障体系。经济上并不是问题，有关部门自己承认，建立一个覆盖全民的类似北京市这样的社会医疗保障体系，只需花费大约为5000－7000亿元，和开奥运会&quot;会&quot;会的花费相当，比每年的维稳支出还要少； 6. 应当取消体制内享有的医疗和养老特权，取消编制，改变现有的社会保障资金主要（&gt;70%）用于领导和高级公务员的健康和生活开支的状况。或许，这才是社会保障制度改革的真正阻力所在。<br />
　　最后也是最重要的建议，计划生育政策决不可以执行两代人的时间。独生子女的子女不能是独生子，否则人口结构的改变是毁灭性并且不可恢复的，中华民族整体将在一个人的自然寿命内的几十年内迅速衰微，这在人类历史上前所未有的。现在第一代独生子女已经过了30岁了，中国应该从现在开始放弃计划生育！<br />
　　而放开也不一定有用，因为当前的住房压力，生活节奏和生活成本已经压得年轻夫妇不愿意多生小孩。即使放开了生，也只有极少数家庭愿意生养2个以上的孩子，还有很多家庭不会生养以及不孕不育。另外一个，少数民族出生人口已经占全国总出生人口的25％，这是2005年抽样的数字。现在可能还要高。可想而知，继续执行现在的汉族严格计划生育，少数民族放开生养的政策，人口问题将威胁到中国的稳定。<br />
　　而现在出来的打工求职者，更为年轻，80％未婚。和他们的父辈不同，他们是不会种地也回不去的新一代。高房价高生活成本让他们丧失了生育权。在未来几年内&quot;我们公司&quot;如果不重视解决这一庞大群体（1－2亿人）面临的问题，这些无法安居婚育的流动的年轻人，将是社会不稳定的导火索。 <br />
