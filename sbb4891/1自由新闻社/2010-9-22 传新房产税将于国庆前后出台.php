　&nbsp;&nbsp;只是,我们堂堂的一个人民&quot;快活&quot;专政的&quot;公私合营&quot;国家,在土地问题上怎么搞得和殖民地时代的香港一样?<br />
　　一直梦想着有一天,能够高声喊出一个响亮的口号:<br />
　　土地,财富和一切权力,属于人民!<br />
　　香港人很惨的.我认识的一个香港同事,96年买房,02年首付陪光倒贴卖出去,现在两个小孩了,还是在租房.<br />
　　这还是香港十多年来几乎唯一一个收入上涨的行业员工的现状.殖民地时代的悲剧,以为回归了会好一点把,我去,祖&quot;宋庆龄&quot;亲也在大力学习港英当局那一套,要不为什么最近港人的&quot;快活&quot;诉求这么强烈. <br />
　　高房价下香港人绝种<br />
　　弹丸之地的香港，只要小孩在那里出生，就给永久居留证，为什么？<br />
　　香港土著每年只出生3万多小孩，80年只出生200多万。而80年现有人口差不多死光。80年就减少2/3的人口，大部分香港人绝种！这就是产生世界级地产富豪的代价！ <br />
　　最近，香港一本名叫《地产霸权》书籍开始热销起来，《地产霸权》一书作者潘慧娴，她是香港新鸿基地产创办人郭得胜的前女助理，后来加入嘉里建设负责土地和物业的评估及收购。简单来讲，《地产霸权》书中内容其实就是讲香港已经被几个大地产商家族所垄断的客观事实，甚至于就连原本正常的政经关系也已被彻底改变。因此，表面上所谓高度自由的香港资本主义经济，其背后实质却是高度垄断之下合理、合法、合情的官商勾结和权钱交易。所以，现在香港之所以也开始出现仇富心态、愤青一簇以及原来香港精神的完全丧失，这其实也跟建立在大地产商垄断香港土地资源，房地产投机造成的高房价和高生活成本密切相关。<br />
　　而投机本身不能创造财富，不过是透支和掠夺全体劳动者的未来收入。这种模式发挥的效果越大，普通劳动者生活的就越痛苦，特别是初入社会的年轻人。总结一下地产财富模式的危害。第一：社会以地产财阀扩张结合&quot;我们公司&quot;意愿为主宰，对内剥削为主，扭曲了整个社会形态和文化价值体系，财富要靠投机，勤劳不能致富。第二：社会经济活动以大宗不动产投机为主体，压制了其他一切行业的创造力。全社会为几个巨阀打工。其他所有行业的利润被脱去一层皮。第三：剥夺了普通人住普通房子的权力，残酷剥削年轻人，使得年轻人普遍买不起房，结不起婚，无法过上安定的生活。第四：更深远的，加速改变了人口结构。实现李嘉诚财富，香港模式的地方，人口出生率一定不足以维持人口自然更替。<br />
　　香港现在每年有7万名小孩出生，其中一半来自大陆内地落地生。香港土著每年只出生3万多名小孩。不知道各位是否知道这个数字意味着什么。以公认的人口负增长，出生率低的日本来相比，按照日本的出生率，香港本土700万土著每年也应当出生7万名以上的小孩。换句话说，香港的实际出生率只有衰亡中的日本的一半，这个城市要绝种！这就是为什么弹丸之地的香港不得不支持落地居民证的原因。如此之低的生育率，和香港高昂的地价和生活成本直接相关。普通人的要求，不过是一间房，一张安息的床，但是却要为这最基本的生活条件为地产财阀打工半辈子，以至于失去了生育权。全体居民如此昂贵的代价，养出了几个世袭的地产富豪！只有他们几个玩得起，房价地价始终掌控在平均收入水平难以企及的高度，其他人都要出局，保证了广大居民世世代代都要为他们打工。任何其他新兴行业由于起始资本的限制，失去了在这个城市发展壮大的可能。这个城市只有期待移民带来新鲜血液，自身在一堆昂贵的钢筋混凝土中渐渐衰朽。整个社会都在渐渐消亡，失去活力。这就是殖民地时期形成并沿用的香港模式，是权贵资本主义中最差的一种形式。华人社会的首富是李嘉诚，而不是比尔盖茨式的新兴富豪，这个社会是不会有什么前途和希望的。<br />
　　现在，全中国都在搞香港模式，经营城市。现在世界房价收入比最离奇的不是香港，而是北京上海深圳等内地一线城市，并且高房价有扩展到二三线城市的趋势。这是中华民族在自我毁灭，相关官员要承担历史责任。中华民族作为世界上唯一幸运的延续五千年的民族，凭借世界第一的人口数量，穿越了无数苦难，艰难的走到现在。如果像现在这样，在全国推开香港模式，效果是在短短数十年内民族自戕，生育率急剧降低，断子绝孙。如果生育率不提高的话，未来十年内，当建国后出生的人口步入死亡年龄时，中国将可能每年人口净减千万。请看我之前的博文“社会，经济，房价与人口”一文的分析。<br />
　　而地方&quot;我们公司&quot;还在拼命拉高房价地价，并且是全国性的。看起来中国的生育率有进一步减低的可能。因为年轻人不可能在年轻的时候有属于自己的居所。到老了成为既得利益阶层了，反而不希望房价下降了。这样，只需持续大约20－50年，一个人的寿命以内，中华民族将完全进入衰落期，以不到2亿年轻人养活4－6亿老人，这个国家要进入死亡的沉静。而可以预料，那时，受过良好教育的年轻人将是世界上最宝贵的财富，而所有发达国家都会放开移民政策，年轻人会大批的流向生活条件更好，资源更丰富的国家。年轻人跑了，会留下一大群老头守着永远不能变现的不动产，彼此照顾，身体好的老人照顾身体不好的老人，因为年轻护士都找不到了。这就是用房地产经济模式发展的中国的未来世界。未富先老，未强先衰，并且人口结构再无改变的可能，直线衰落。这就是地产经济加上计划生育下中华民族的命运。<br />
　　反思房地产投机以及这些地产巨阀的危害，要做的其实很简单。必须在制度上杜绝任何地产投机获利的可能。比如借鉴美国，德国的成熟房地产交易制度，在持有税之外，多套房产投机交易要交纳50％以上的交易税，保证了囤房囤地不可能致富，根本不可能靠地产投机成为数一数二的富翁。而社会财富要靠发掘生产力而来。这才是健康的社会发展道路。对于那些带来新生产力的财团，&quot;我们公司&quot;要大力支持，而对于那些投机为主的财阀，&quot;我们公司&quot;应当课以重税，限制发展。财产税和遗产税必须开始征收，确保这些投机巨阀不能连续做大。这是保障社会基本公平，维持社会基本活力的必要前提。必须批判李嘉诚的财富模式，抛弃罪恶的土地GDP，还土地以正常的使用价值。这是整个中华民族得以顺利延续的必要条件。 <br />
　　每日经济新闻9月29日讯 在两部委刚刚出台“151号文”之际，昨日，一则关于8部委国庆前出台新调控政策的传闻，又在房地产业界引起轩然大波。<br />
　　到目前为止，“国十条”中曾提及的房产税并未推行，这引发了各界对税收等政策继续出台的猜想。<br />
　　截至目前，主管部门并未对此传言做出任何回应。但昨日晚间，一位接近决策层的人士向 《每日经济新闻》记者表示，“传言是真的，明天（9月29日)之前有可能出来”。<br />
　　传闻从27日（建设部、国土部联合出台“151号文”的次日）便开始在业界传播。据称，新一轮调控方案的基本思路是“遏制投机购房需求，加快增加住房有效供给，并力求将短期调控的措施与中长期的发展相结合。”<br />
　　昨日晚间，上述接近决策层的人士向本报记者确认了上述消息，并表示，新政由多个部门联合出台，比此前更加严厉。其中，作为政策“最后的砝码”的房产税可能提前亮出。该人士表示，新政将确定几个城市推行房产税试点。同时，二套房和多套房信贷政策将“进一步收紧”。<br />
　　“如果真是这样，&quot;我们单位&quot;调控房价的决心之大，真的超出了大部分业内人士的估计。”上海易居房地产研究院综合研究部部长杨红旭表示。<br />
　　事实上，根据本报记者的采访，多数业内人士认为，在两部委刚刚发文调控市场的情况下，马上出台新政的可能性并不大。&quot;我们单位&quot;财经大学税务学院副院长刘桓向《每日经济新闻》记者表示，多数地区并未很好地执行“国十条”，且两部委刚刚出台细化政策，从这个层面上讲，此时没有出台房产税的必要。<br />
　　一位接近国土部的人士向 《每日经济新闻》记者表示，在多数公众看来，本轮调控并未达到效果这届&quot;我们公司&quot;最大的问题不在于把经济搞的多差，而在于不但不控制投机，反而完全站在投机者一边，把整个社会的正常观念翻转了。十年之前，大家认为改善个人生活水平的最好方法是投身于能给社会带来价值的行业，大家的理想职业是白领，企业主。现在，基本没有人再认为给社会带来价值的同时可以改善个人生活水平了，大家在炒房，炒股票，炒期货，炒作一切可以炒作的东西，玩击鼓传花游戏。实体经济已经被人们忘记了。一句话就是：没人再愿意为社会创造价值了。因为投机赚钱，不投机亏本。<br />
　　社会实际价值不增长，其他纸面上的东西再怎么增长总有一天也会崩塌的。这将是中国最深重的危机。这届&quot;我们公司&quot;的所作所为相当于发动了一场思想的文化大&quot;过家家&quot;，而带给中国人的不良倾向将在今后很久都被批判。<br />
　　根据福布斯富豪榜统计，中国大约有70多万千万富翁，其中绝大多数和房地产有关，靠房地产，靠职业炒房迅速攫取了相当于全体国民若干年的收入总和的巨额财富。有这样的房产财富打底，中国的社会公平，分配公平如何谈起？&quot;我们公司&quot;如何收拾这样的财富格局？好像这些人基本都要移民了，兜里带外国护照的多。留下全体中国人辛勤工作若干年为他们还账。<br />
　　财富全靠投机，勤劳不能致富，&quot;我们公司&quot;有责任。 <br />
