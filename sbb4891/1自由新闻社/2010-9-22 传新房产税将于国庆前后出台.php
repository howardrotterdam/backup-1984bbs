　　北京的房价在什么情况下可以再涨，甚至大幅度上涨？跨国的房价比较的基础是汇率，而这是最不可靠的。只能说，以一个长的周期看，比如看10年以后的北京房价，那么参照东京房价就可以算一个参考的天花板价格，如果汇率水平基本不变，那么显然北京的房价长期稳定的上升空间是很小的。如果房价10年后比现在有大幅上升，那么有两种可能：<br />
　　（1）国际上，特别是日本发生了大规模通货膨胀，导致可比房价上升。（考虑到西方成熟市场的相对稳定性，似乎可能性偏小）。<br />
　　（2）人民币汇率发生大幅贬值，提升了北京可比房价的人民币价格上升空间（考虑到国内潜在的通货膨胀压力，这倒是很有可能）。<br />
　　当然，10年内人民币实现资本项目下可自由兑换或许是可以直接比较房价的前提条件。其它情况下，看不出以一个长周期的角度，目前房价还有什么大幅上升的动力。无论如何，有一件事情似乎是可推断的，也就是国际资本流入现在的帝都房产市场特别是住宅市场的可能性不大。从逐利角度看，人民币升值将导致长期看国内房价的可比价值变小，而人民币贬值则直接让流入的资本发生损失。即使人民币汇率不变，国际资本投资中国居住市场的获利空间和安全边际也不够大。 <br />
　　房地产经济最危险的后果：民心尽失<br />
　　中国有一句成语：成也萧何败也萧何，房地产对中国的发展就充当了一个这样的角色。特殊政策造就下的房地产推动了中国经济的发展，成为最具发展快的行业和国家发展的标志，同时房地产摧毁了国民信心，是社会积怨之根。现实的房地产成了一块烫手的山芋，并且还具有粘性，你想撒手现在都根本不可能。不得不承认中国很难。这种难不在于外部，恰恰来自于内部，来自于给予了中国一时繁荣和表面繁荣的房地产，正是它弱化了国人的神经，正是它把国人的忍耐力和最大的希望都达到了临界状态，一触即发，一发即爆。尽管正面舆论所描述的是歌舞升平一派盛世风光，但更多的人可能已经闻到了因房地产导致的内部硝烟味。<br />
　　内部环境的变化导致外交的被动，因为国内的压力，没有足够的时间让你从容的走一盘棋。当民心稳定，当信任度加大的时候，即便日本出兵占领钓鱼岛，即便美国航母真的开进黄海军演，对中国来说都不是可怕的，中国有时间和西方斗智斗勇。如果中国一强硬，军方稍微有所动作，对立的国家都会有所收敛的（尽管我们的动作不多，高层是否有所计议尚在猜疑中），民间急剧强烈的抗议呼声，也不得不是对立国家所考虑的，毕竟引发十多亿人的反对，对几亿人的国家来说，还是有威慑力的。加之经济一体化背景下，你中有我，我中有你，掣肘的地方总是很多，经济报复的办法或许比战争的意义更大，更深远。但是国内开始形成的不信任，不允许你这么做了，不动武力成了软弱的表现。这种不信任很大程度上就是房地产暴掠既成事实和调控无果造成的，&quot;我们公司&quot;在吞一杯难咽的酒。<br />
　　对比08年和10年两次国内重灾，很多人面对捐款的态度，可以直接看出民心向背。08年很多人是主动捐款，抢着捐款。到了10年，大部分人被捐款。为啥，和房价比起来，都意识到自己是穷人。很多人公开说，zf发的地产财不小，还用得着我去捐这么点吗？前几天在一个车站附近，听到一群人在议论美军航母开到黄海。一个人大笑说，啥时候开到天津港，我去给美军带路。如此反动叛国的言论，如果是10年前甚至5年前，都可能会激起一群人的公开声讨。但是现在，居然有人叫好，这个说，被美军解放似乎也不错。那个说，美军来了，也许我就买得起房了。 <br />
　　zf已经失去了国内的民心优势。这个优势失去的罪魁祸首是房地产，严格的说是最近两年执行的房地产政策，激化了社会矛盾，彻底改变了中国的价值和道德观念，至此中国人缺失了任何信仰，唯利而已。民众的团结，社会的安稳，国家的威信受到了严重的考究，传统的人心向善和克制内敛被严重冲击，很难再有国家利益为主和顾全大局的概念。而一点点的风吹草动，外忧内患就把相关部门架在了火上，进一步被动。<br />
　　现实的房地产政策完全扭曲了普通老百姓的人格，破坏了老百姓期望的安定平和的生活环境，多少年肆无忌惮的深重掠夺和可能将要发生的再度疯狂，期望和现实依然如故甚至变本加厉回升的巨大反差，以及伴随于房地产过程所引发的一系列暴力事件，使得人们的心理承受力越来越差。往往这个时候zf期望从容下一盘棋的做法，很容易被国人误解。<br />
　　应该讲果敢的威慑，快速的反应完全可以将各种事件消除在萌芽状态，但威慑的背后是已经准备好的武力，不得不承认目前国内已经出现的一系列矛盾，严重分散了&quot;我们公司&quot;精力，至少是如履薄冰战战兢兢，为此对这一系列事件处理和应急很难从容，底气不足完全可以理解。对于稍微有思考和良知的国人，或许评论时局是一种杞人忧天，更可能被认为是眼光低劣，甚至是捣蛋，面临封杀和控制的局面，但面对这种既成事实和无可奈何现实，却很难去爱什么了，去崇尚什么和相信权威了。这无疑将激发和加剧了一个民族的仇恨，加剧不信任，各种不利于团结稳定的因素愈发加倍潜伏和累计，爆发的几率极大提高，这才是对中华民族最大的危害，甚至达到了离间和分化中国的目的。因为没有处理好自己的内政和民生的事情，一个很小的事件和动作就可能激化&quot;我们公司&quot;和百姓的矛盾，一股强大的力量被化解成内部的积怨，形成两股可能是相左的力量，继续内耗，最后让外敌坐收渔利。这就是失去民心的后果。<br />
　　中国面对了许多的自然灾难，但所谓多难兴邦，对发展中的中国其实没有根本的危害，跌倒了可以站起来，损坏了可以重建，但民族团结和国家信任力的降低，却不是一时半刻就可以弥补的，原则说我们的难来自于内部，这种软性的灾难在于内部离心的尴尬局面下，外因稍一刺激就越来越严重，愈发到了无法控制的局面。这才是国家的根本，远比GDP数字更为重要。<br />
　　房地产已经完全不是经济问题了，房地产引发的严重社会问题不仅仅会表现在和外部的冲突上，其实想一下，国内最近发生的一些千奇百怪的事情，那一件不是国人人格的扭曲造成的。利益的畸形分配，让每个人重新审视自己，重新衡量自己在社会中的地位，原来看似公平合理的东西都不合理了。一切在判断和甄别上错位，一切都在否定当中。房地产和土地政策在给zf和相关利益集团带来收益的同时，也把zf自己推向了绝路。在自己获得执政上财富的同时，让更多的人陷入贫困，失去了更多的民心，所有社会问题都由最高zf买单。那么如果那一天承担不起了，将会是怎么样的局面？老百姓怎么样去维护zf？怎么样爱国？更怎么样面对所谓的领导？又怎么样去理解zf一盘棋的良苦用心？就比如这次的撞船事件，已经把所有的积怨都加在zf身上了，根本就不可能容忍可能是最好的，也是外交和经济的用兵伐谋，达到不占而屈人之兵的境界，即便于你有一万个理由，一万个利益核算对比，也不可能给你时间和理解。<br />
　　政治者民心也，获得了民心，做什么都是从容的，更是有基础的，所以首先应该检讨，民心是怎么失的。笔者从来不上升到人身的角度和品行的问题去看相关领导，去攻击什么, 至今还是认为中国的问题很大程度上是一系列欠考虑的房地产政策和后来出现问题的不作为造成的。即便日本不犯事，在别的方面还有更大的尴尬。因为房地产政策彻彻底底突破了老百姓的心理底线，彻彻底底践踏了老百姓的人格，也就彻彻底底改变了中国人的心理构成。<br />
　　人心散了，队伍不好带。房地产彻底改变了中国，是兴盛了中国还是衰败了中国，其实答案早出来了。每个人都要为自己做的事情负责，相关zf官员也不例外。大国政治，岂能儿戏。必须追究相关官员的责任，挽回民心。否则，维稳是维不稳的。卸任之后，名利双收，和老婆孩子一起数钱，世界上恐怕再也没有这么便宜的事。 <br />
　　要小心看现在的回暖。最近的所谓回暖炒做迹象太严重了 <br />
　　几乎所有的房地产销售数据都谈环比,没有同比.对于这样一个季节性因素超强的市场,实在是有点说不过去.<br />
