													<h2>财政部与税务总局称有必要对个人所有住房恢复征收房产税</h2>
						
						
																											<div id="postmessage_851695" class="t_msgfont">财政部与税务总局称有必要对个人所有住房恢复征收房产税<br />
2010年09月30日 20:08中国新闻网<br />
中新社北京9月30日电(记者 赵建华)中国财政部、国家税务总局有关负责人30日表示，对个人所有的住房恢复征收房产税是必要的。这既有利于调节居民收入和财富分配，也有利于健全地方税体系，促进经济结构调整及土地节约集约利用，引导个人合理住房消费。<br />
<br />
该负责人介绍说，从国际经验及公平、规范的角度看，房产税应对个人所有的住房普遍征收。但考虑到这项改革情况复杂，拟先在部分城市对部分个人拥有的住房进行试点，恢复征收房产税，积累经验后逐步扩大到全国。<br />
<br />
该负责人称，中国现行房产税开征于1986年。基于当时城镇居民收入水平普遍较低等情况，《中国房产税暂行条例》规定，对个人所有的非营业用房产免税。<br />
<br />
该负责人还介绍说，经国务院批准，财政部、国家税务总局、住房城乡建设部9月29日联合发出通知，调整房地产交易环节契税和个人所得税优惠政策。<br />
<br />
根据新政策，官方将对个人购买自用普通住房减半征收契税调整为对个人购买属于家庭唯一住房的普通住房，减半征收契税；对个人首次购买90平方米及以下普通住房减按1%税率征收契税，调整为对个人购买90平方米及以下且属于家庭唯一住房的普通住房，减按1%税率征收契税。<br />
<br />
同时，对个人转让自用5年以上、并且是家庭唯一生活用房取得的所得，继续免征个人所得税；对出售自有住房并在1年内重新购房的纳税人不再减免个人所得税。<br />
<br />
该负责人表示，此次调整旨在保证居民基本居住需求的同时，合理引导住房消费，抑制投机性购房。</div>

							
							
							
							
