													<h2>宁高宁学习风波：收购蒙牛三大疑问再被提及</h2>
						
						
																											<div id="postmessage_175639" class="t_msgfont">联合厚朴基金以61亿港元收购蒙牛20%股权的中粮集团董事长宁高宁，突然陷入一场“停职学习”风波。<br />
<br />
7月22日据某门户网站报道，来自中粮内部的消息称，中粮集团董事长宁高宁已接上级调令“外出学习”，集团职务和待遇目前不变，但不参与集团事务。正式文件近日将下发，目前此事还未内部通报。<br />
<br />
中粮集团一位内部人士告诉本报记者，宁高宁得知该消息后很愤怒。“宁董亲自要求对这个传闻发出正式声明，还要亲自审阅声明。”<br />
<br />
22日下午，中粮集团发表声明，“报道内容纯属捏造，完全不符合事实。”<br />
<br />
22日晚上9点，某门户网站登载的中粮集团声明被删除。中粮一位负责人对此表示，事件并无变化。有消息称，中组部即将在中粮集团召开高层会议。本报记者也在中粮福临门大厦一楼的显示屏上看见“欢迎中组部领导”等字眼。不过上述负责人表示，中组部召开的会议跟“学习”事件并无关联。<br />
<br />
与此同时，关于中粮入主蒙牛的收购案的三大存疑再次被业内提及。<br />
<br />
巧合的是，7月17日蒙牛宣布，中止总额为16.46亿港元的员工股权激励计划，当时，距该计划推出仅仅8天时间。<br />
<br />
蒙牛的解释是：公司董事会收到了多位相关人士反馈，经过再三讨论后决定中止执行该股权激励方案。对于这件事是否与中粮有关，中粮方面没有表态。<br />
<br />
宁高宁行程仍满满<br />
<br />
7月22日下午6点，北京朝阳门南大街东侧的中粮福临门大厦。中粮集团总部刚刚搬迁到此。已到下班时间，雨下得太大的缘故，不少中粮下班的员工都等候在大厦大厅和门口。大家谈论的话题主要围绕突如其来的大雨、上午的日食或者晚饭的去处。<br />
<br />
“今天宁总有过来啊。”大厦门口的保安告诉记者，上午十点多，宁高宁步行来上班，还跟保安打了个招呼。<br />
<br />
中粮集团公关部一位负责人则表示，宁高宁晚上有个接待活动，5点半就离开了。“如果他在，一定跟你见面，我们没必要说谎。”<br />
<br />
据了解，宁高宁今天还与中粮集团总裁于旭波一起会见了邯郸市政府的相关人员。7月21日，宁高宁还与于旭波、薛国平、曲础⑼踅鸩等中粮集团高层在中粮福临门大厦出席了国资委召开的“中央企业负责人视频会议”。<br />
<br />
根据某门户网站的报道，“自从上周出差深圳归来后，宁高宁就不像以往那样活跃在集团的各种工作场合”。<br />
<br />
61亿收购案的三大存疑<br />
<br />
该门户网站还报道称，“宁高宁停职学习一事或与收购蒙牛交易相关，但具体是哪个环节出现问题，尚未得知”。<br />
<br />
7月6日，中粮联合厚朴基金以61亿港元收购蒙牛乳业20%股权，至今未及一个月。此后收购案三大存疑却开始流传。<br />
<br />
首要存疑是收购价格。根据交易公告显示，每股17.6元的交易价格，相对于2009年7月3日（即《认股协议》签订前最后一个交易日）19.10港元每股的市价折让约7.85%；与蒙牛2008年每股最高价格26.3元比较，差距并不大。<br />
<br />
关于交易价格，宁高宁曾表示“我们也想在价格低时候买，但是人家不卖。”<br />
<br />
价格存疑的背后仍是财务视角问题。2008年蒙牛乳业的净资产是47.38亿，61亿的现金收购远超出了蒙牛乳业的净资产总额，何况这笔巨额现金还仅是获得20%的股权。<br />
<br />
在这场被称为“国进民退”的交易中，这场看似纯粹的市场交易，因不同的财务视角，无形中将“国资”中粮，推向被质疑的前台。<br />
<br />
再就是交易的决策过程。毕竟这次中粮将为此拿出真金白银40多亿。但事后的一些解释，反而为此平添了疑虑。<br />
<br />
牛根生没有掩饰他与宁高宁的熟悉关系。“宁总是中国企业家俱乐部荣誉主席，我是轮值主席，去年交流比较频繁，所以一拍即合。”<br />
<br />
“一拍即合！”宁高宁和蒙牛董事长牛根生都愿意用这句话来概括双方谈判的过程。据了解，中粮与蒙牛的谈判始于2009年3月，虽然在数次谈判期间，双方也曾有反复，但仍在短时间内敲定细节。由于宁、牛二人熟识，双方合作甚至没有中间介绍人。<br />
<br />
为何不入主管理层则是第三个存疑。按照双方协议，在蒙牛未来的董事会11名董事中，中粮虽然将占有3个名额，但均为非执行董事。<br />
<br />
宁高宁此前在接受本报记者采访时也明确表示，中粮不参与蒙牛的具体经营管理，不改变现有的经营团队的连续性和稳定性，也不改变目前的战略方向。按照宁高宁目前希望的与蒙牛管理层的合作方式，中粮将通过董事会对蒙牛发展的大方向、大政策产生影响，并推动蒙牛和中粮在业务上的合作。<br />
