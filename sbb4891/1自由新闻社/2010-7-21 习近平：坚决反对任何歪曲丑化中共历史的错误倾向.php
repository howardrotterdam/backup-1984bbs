													<h2>习近平：坚持实事求是研究和宣传中共历史</h2>
						
						
																											<div id="postmessage_752743" class="t_msgfont">2010年07月21日 20:23　来源：中国新闻网<br />
<br />
　　中新社北京7月21日电 全国党史工作会议21日在北京人民大会堂举行。会前，中共中央总书记胡锦涛亲切会见与会代表。中共中央政治局常委、中央书记处书记习近平强调，坚持实事求是研究和宣传党的历史。<br />
<br />
　　习近平在会议上指出，中国共产党是经历革命、建设、改革长期考验，在异常复杂环境中团结带领中国各族人民创造了伟大奇迹的党。建党89年来，中共为民族独立、人民解放和国家富强、人民幸福付出最大牺牲，作出最大贡献。深入研究党的历史，认真学习党的历史，全面宣传党的历史，充分发挥党的历史以史鉴今、资政育人的作用，是中共和国家工作大局中一项十分重要的工作。<br />
<br />
　　习近平强调，坚持实事求是研究和宣传中共历史，要牢牢把握中共历史发展的主题和主线、主流和本质，旗帜鲜明地揭示和宣传中国共产党在中国的领导地位和核心作用形成的历史必然性，揭示和宣传中国人民走上社会主义道路的历史必然性，揭示和宣传通过改革开放和社会主义现代化建设实现中华民族伟大复兴的历史必然性，揭示和宣传党在革命、建设、改革各个历史时期领导人民所取得的伟大胜利和辉煌成就，揭示和宣传中共在长期奋斗中积累的宝贵经验、形成的光荣传统和优良作风，坚决反对任何歪曲和丑化党的历史的错误倾向。<br />
<br />
　　习近平强调，中共的历史是一部丰富生动的教科书。用党的历史教育党员、教育干部、教育群众尤其是教育青少年，是党史工作服务党和国家大局的重要内容。<br />
<br />
　　习近平指出，党史研究是一门研究中共的历史、从中共的活动揭示当代中国社会运动规律的科学。既要坚持和发展马克思主义史学研究的优良传统，坚持和发展党史工作积累的成功经验和方法，也要吸收借鉴古今中外史学研究的有益经验和方法，还要积极运用现代科学技术，创新党史研究的手段、方法、载体。要注重发挥互联网等现代传媒在人们工作和生活中的独特作用，加大党史宣传教育和党史知识普及力度。</div>

							
							
							
							
