7月21日，全国党史工作会议在北京人民大会堂举行。会前，党和国家领导人胡锦涛、李长春、习近平、贺国强等亲切会见与会代表并与代表合影。新华社记者庞兴雷摄<br />
<br />
新华网北京7月21日报道 全国党史工作会议21日在北京人民大会堂举行。会前,中共中央总书记、国家主席、中央军委主席胡锦涛亲切会见与会代表,向会议的召开表示热烈祝贺,向全国党史工作者致以崇高的敬意和诚挚的问候。<br />
<br />
中共中央政治局常委李长春参加会见。中共中央政治局常委、中央书记处书记、国家副主席习近平,中共中央政治局常委、中央纪委书记贺国强参加会见并出席会议。<br />
<br />
上午9时30分,当胡锦涛等中央领导同志来到人民大会堂北大厅同出席会议的代表见面时,全场响起热烈掌声。胡锦涛等高兴地与代表们亲切握手,热情交谈。<br />
<br />
习近平在会议上作重要讲话,他讲了五个问题:一、进一步提高对党史工作重要性的认识;二、坚持实事求是研究和宣传党的历史;三、加强党的历史的学习和教育;四、努力提高党史工作的科学化水平;五、切实加强对党史工作的领导。<br />
<br />
习近平指出,中国共产党是经历革命、建设、改革长期考验,在异常复杂环境中团结带领我国各族人民创造了伟大奇迹的党。建党89年来,我们党为民族独立、人民解放和国家富强、人民幸福付出最大牺牲,作出最大贡献。深入研究党的历史,认真学习党的历史,全面宣传党的历史,充分发挥党的历史以史鉴今、资政育人的作用,是党和国家工作大局中一项十分重要的工作。今天,党和国家的事业正沿着中国特色社会主义道路不断向前迈进,新的形势对党史工作提出新的任务,要求党史工作进一步提供历史经验,更好地为现实服务。<br />
<br />
习近平强调,坚持实事求是研究和宣传党的历史,要牢牢把握党的历史发展的主题和主线、主流和本质,旗帜鲜明地揭示和宣传中国共产党在中国的领导地位和核心作用形成的历史必然性,揭示和宣传中国人民走上社会主义道路的历史必然性,揭示和宣传通过改革开放和社会主义现代化建设实现中华民族伟大复兴的历史必然性,揭示和宣传党在革命、建设、改革各个历史时期领导人民所取得的伟大胜利和辉煌成就,揭示和宣传党在长期奋斗中积累的宝贵经验、形成的光荣传统和优良作风,坚决反对任何歪曲和丑化党的历史的错误倾向。这是党史工作必须遵循的党性原则,也是每一个党史工作者应该履行的政治责任。<br />
<br />
习近平强调,中国共产党的历史是一部丰富生动的教科书。用党的历史教育党员、教育干部、教育群众尤其是教育青少年,是党史工作服务党和国家大局的重要内容。要以各级党员领导干部为重点,把党史教育纳入干部教育培训的必修课,把全面了解和正确认识党的历史作为一项基本要求,教育引导党员领导干部特别是年轻干部认真学习党的历史,努力提高思想政治素质和领导水平。要着力抓好青少年这个群体,开展形式多样的党的历史知识、光荣传统和优良作风、英雄模范事迹的教育,积极推动党史教育进学校、进课堂、进学生头脑,从小培养青少年热爱党、热爱社会主义的感情。<br />
<br />
习近平指出,党史研究是一门研究中国共产党的历史、从中国共产党的活动揭示当代中国社会运动规律的科学,要坚持党性和科学性的统一,党史研究工作者遵守党的政治纪律、宣传纪律和充分发挥个人创造性的统一。既要坚持和发展马克思主义史学研究的优良传统,坚持和发展党史工作积累的成功经验和方法,也要吸收借鉴古今中外史学研究的有益经验和方法,还要积极运用现代科学技术,创新党史研究的手段、方法、载体。要注重发挥互联网等现代传媒在人们工作和生活中的独特作用,加大党史宣传教育和党史知识普及力度。<br />
<br />
<br />
习近平强调,各级党委要把党史工作摆上重要议事日程,认真贯彻落实中央关于党史工作的指示精神,积极关心党史工作,对加强和做好党史工作提出明确要求,及时研究解决党史工作中的重大问题,推动党史工作各项任务的落实。<br />
<br />
会议由李源潮主持。会上宣读了有关表彰决定,为32个全国党史系统先进集体和12名先进个人颁了奖。<br />
<br />
