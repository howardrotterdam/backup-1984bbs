													<h2>全国党史系统先进工作者先进个人素描</h2>
						
						
																											<div id="postmessage_752756" class="t_msgfont">2010年07月21日19:08&nbsp;&nbsp;来源：新华社<br />
<br />
　　他们是默默无闻的一群人，他们是信念坚定的一群人。他们，就是来自基层的党史工作者，为着神圣而崇高的党史事业甘于奉献，矢志不渝。<br />
<br />
　　２１日，北京人民大会堂，激越的乐曲声中，几十位全国党史系统先进工作者和先进个人胸戴红花，代表全国１．７万多名党史工作者获得表彰，接受荣誉。<br />
<br />
　　多年来，广大党史工作者爱岗敬业、锐意进取，不断开创党史工作新局面。走近他们，就会真切地感受到党史事业使命的光荣，责任的重大。<br />
<br />
　　今年５７岁的王慧峰以饱满的精神走上领奖台，但她却曾因为工作的操劳住进了医院。王慧峰现任内蒙古自治区党委党史研究室副调研员，已从事党史编研工作２７年。她几十年如一日，埋头苦干，从不计较个人名利，以朴实无华的实际行动证明了一名共产党员应有的品质。<br />
<br />
　　近几年，因为年龄较大，任务繁重，生活不规律，王慧峰经常胃痛。由于自己参与的电视专题片一直处于紧张的编写过程中，胃痛时她只是自己买点药吃。２００８年１０月，专题片在电视台顺利播出后，王慧峰因胃病加重才住院治疗。<br />
<br />
　　把党史事业看得重于泰山的又何止一人。安徽省马鞍山市委党史研究室主任任仕金年仅４７岁，曾是一名癌症患者，做过膀胱肿瘤手术，组织上让他多注意调养，他却未放在心上。“单位就５个人，自己怎能当甩手掌柜，必须既当指挥员，又当战斗员。”任仕金常常这样说。<br />
<br />
　　新疆生产建设兵团农八师一三六团史志办主任蒋平在身患绝症的情况下，仍默默耕耘在党史岗位上，与生命赛跑，竭尽全力做好本职工作。一些好心人劝他说，你都已经这样了，不要再干了，早点退休算了。蒋平却说：“我只是想在有限的时间里多做一些事。”<br />
<br />
　　这就是基层党史工作者，他们甘于寂寞，无私奉献，不计名利，不计报酬，他们的信念简单而实际――作为一名共产党员，做好党史工作义不容辞，即使倒下，也不放弃。<br />
<br />
　　湖北省荆州市委党史办公室副编审钟鼎有着对党史事业的无限痴情。钟鼎曾有机会调到待遇优厚的银行等其他部门工作，但他却割舍不下手上的研究课题。多年来，他徒步深入边远农村进行党史调查，探访老革命根据地群众１０００多人，征集、整理党史资料１０００多万字。为了赶写书稿，他几乎没有过公休假。长年累月的伏案工作使钟鼎患上严重的颈椎病，他又发明了“望天看书”的方法，坚持工作。<br />
<br />
　　这些平时勤勤恳恳、默默无闻的党史工作者在这一天获得隆重的表彰，自然难掩心中的激动。江苏省连云港市委党史工作办公室主任李东说：“荣誉带来的只是激励，今后我们会更加勤奋努力，锐意进取，无怨无悔地做好党史工作。”<br />
<br />
　　还处于病后恢复期的蒋平用沙哑的声音对记者说：“我现在听和说都很吃力，但我还能写。我要对得起这份荣誉，回去加班加点，争取早日出版我的最新党史研究书籍。”<br />
<br />
　　一个民族如果忘记了自己的历史，就不能深刻地了解现在，不能正确地走向未来。为了记录党的历史，为了宣传党的历史，一代又一代党史工作者在奉献的同时，不断开创出党史工作的新方法、新途径，他们同样获得了表彰。<br />
<br />
