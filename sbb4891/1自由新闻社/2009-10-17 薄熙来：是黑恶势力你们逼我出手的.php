美国《纽约时报》又给中国上眼药：“俄罗斯领导人视中国为执政楷模”；首先感到舒服，并眯起眼睛来的是中国的《环球时报》：“‘中国模式’再被热议”。“中国模式”已经嘈嘈多年了，它在许多不同的语境中和含义里被使用，使用最多的是两种，一种是动态的操作模式，主要指的是“改革模式”；另一种是静态的体制模式，也就是《纽约时报》这篇文章所说的“统治模式”。<br />
<br />
中国的改革模式是邓小平设计的，是“摸着石头过河”的渐进改革模式，与休克疗法的前苏联激进改革模式形成对比。所以，渐进改革模式，也可以称为“中国摸式”，摸着石头过河的“摸”。这个“摸式”，与盲人用拐杖点路很相似。唐人段成式在《酉阳杂俎续集・支诺皋上》里化用了大思想家扬雄用过的一个词：“`植索涂”（扬雄用的是“`埴索涂”），说，“方蒙袂辑履，有愤于黔娄，`植索涂，见称于扬子，差不同耳。”意思是说，别小看那些衣履不整，甚至用拐杖探路的瞎子，他们中可有些像黔娄那样的乞丐卿相，白衣圣贤呢。<br />
<br />
我想说的不是这个意思，我的意思是，中国改革在初期，就像个“`植索涂”的盲人一样，用拐杖左点一下，右点一下，中间点一下，然后把脚迈向比较平坦的那一点。这在哲学上叫“试错法”。中国改革最成功的两条经验，就是“两试法”：一是“试错”，二是“试点”。用试点来试错，失败了成本低，成功了收益大。从这个意义上说，中国改革的成功，得益于这个意义的“中国摸式”，瞎子摸路少摔跤。<br />
<br />
但是，如果把中国模式设想为框架性的政治经济体制，认为中国故事是“自上世纪30年代以来‘自由民主所面临的最严峻挑战’”，把它理想化为自由民主的替代模式，那就有点忽悠我们了，给我们灌迷魂汤，打麻醉药，点鸦片烟。如果我们自己也这么认为，那就是在麻将桌上，或者在被窝里自摸，梦想自摸一张好牌，和了；或者自摸那话儿，哼哼叽叽，神魂颠倒。这种中国式摸法要不得。<br />
<br />
中国目前社会形态，说白了，就是货币社会早期，权力社会晚期。前者富有活力，但也冷酷残忍；后者生机萌发，但也腐朽衰败。或者换个说法，是市场经济加威权政治，这个模式有什么新鲜？民主化前的韩国、台湾，不都是这个模式？“亚洲四小龙”的经济奇迹，言犹在耳，包括马来西亚前总理马哈蒂尔自吹自擂的“亚洲价值”，或亚洲模式哪里去了？中国规模比那些“小龙”规模大了几十倍，经济总量膨胀，这很正常，千万别自我膨胀。<br />
<br />
自摸容易昏头，动作虽小，兹事体大，不可不慎。<br />
