													<h2>七旬反革命犯服刑57年后获释</h2>
						
						
																											<div id="postmessage_777977" class="t_msgfont">57年！中国服刑时间最长的反革命犯77岁终于获释 南方都市报<br />
<br />
　　57年，他可能是云南省乃至中国服刑时间最长的“反革命”犯，在国外也属罕见。一个月前，77岁的他获释，被人搀扶着走出官渡监狱，家乡敬老院收留了他，可是衰老的身躯和淡漠的亲情，让他只能整日蜷缩在床上，就像来到另一个监狱。自由和故土就在门槛之外，这么近，却又那么远。 <br />
<br />
　　他的刑期为何长达57年，在牢狱中怎样度过漫长岁月？处于中国历史上最大变革期的这57年风雨，是否也逾越高墙波及其命运流转？57年之后又如何面对一个全新世界？他会是电影《肖申克的救赎》里那个被体制化然后又被自由所杀的假释老头吗？<br />
<br />
　　新街镇敬老院坐落在镇郊田野中一棵巨大缅树下。它借用了隔壁一座残败古庙的一角，泥墙上挂的还是撤乡并镇前的“新街乡敬老院”木牌，储物柜外侧可见30年前漆写的“新跃公社”字样。正在膨胀的小镇上的居民少人知道它的存在，它似乎活在时光与尘世之外。6月28日，这里来了一个同样命运的老人。两名监狱警察开车从几百公里外把他送来，恳请敬老院收留：“他以前犯有反革命罪，坐了57年牢，现在释放，可是家里人都死了。”<br />
<br />
　　老人被安置在小院东北角烤火屋的隔间里，曾先后有4名老人在此度过最后时光。现任主人离终老也并不遥远，他佝偻得厉害，双腿像竹竿一样细，需要扶着旁物才能走一点路。工作人员买来一个塑料小桶做夜壶，这样就不用去院外的旱厕。用塑料袋包了一件毛衣，把口子扎上，让他坐地上时垫着，还可以手拉着袋口在地上挪动。<br />
<br />
　　他来这里10天后，我们首次前去敬老院探望。他正这般姿势坐在地上抽烟，地上散落着一些烟头。他瘦骨嶙峋，牙齿几近掉光，但目光依然犀利。大家扶他坐在凳子上，给他点上一支烟，他吸了一半就把火掐灭，独自发呆。“你出去多少年了？”我们凑近他耳朵大声问。“十多年了。”“你知道你现在在哪吗？”“在家了嘛！”这时他脸上露出一点笑容。再问他多大年纪，他回答说“二十几了”，还说“想做点事业”，继而用含混不清的语言说了些什么，大家都没听懂。<br />
<br />
　　我们递给他一个本子和一支笔。“欧树。”他抖着手，两次写下他的名字，再慢吞吞加上一句“老欧感谢政府与干部”，全是繁体字。然后继续发呆。<br />
<br />
　　“他有时清醒有时糊涂。”敬老院院长戴学义说。更多时候，他坐在床上，斜靠着墙，眼睛似闭非闭，看着白昼升起和黑夜沉降。他如果在想着往事，往事就像蚊帐里的苍蝇，嗡嗡地在脑海里进出。<br />
<br />
　　6月27日早上，欧树吃过最后一顿囚餐，脱去深蓝色囚衣，换上监狱买来的深蓝色西服，穿上崭新布鞋，把57年的全部家当――几件衣服，几页减刑裁定书，一顶毛线帽，几张说不清来由的纸，一张闲来用铅笔写上“伍圆”再加个线框的“纸币”，几片药板――装进一个深蓝色旅行包。两名警官和一名司机带着速效救心丸送他上路了。<br />
<br />
　　欧树不知道，他可能是云南省乃至中国服刑时间最长的“反革命”犯，在国外也属罕见。2007年，英国一名被判终身监禁的犯人病死在监狱，因坐牢55年居全英之最，而被世界媒体广为报道。<br />
<br />
　　车特意开得很慢，欧树不停抽着烟，一直盯着窗外。傍晚终于到达大理州弥渡县新街镇，这就是他的家乡。当新街派出所所长用家乡话欢迎他回家时，他喜笑颜开。<br />
<br />
　　欧树在当地已无任何资料，镇政府从来没有面对这样的事，第二天一早召集各部门紧急磋商，决定安排他住进镇敬老院，日后再申请办理“五保户”。派出所当天为他办理了户口，“宗教信仰”一栏注明“无”。他小心翼翼想把户口本放进胸口的西服内袋，可是手抖得厉害。随行警官帮他放好，系上扣子，他又紧了紧衣领。<br />
<br />
　　他被搀扶着走进镇敬老院。安置好后，警官正要离开，他突然站起来似乎想要跟着走。警官扶他坐回去，告诉他：“你到家了，就在这里安享晚年！”他攥紧警官的手，舍不得放开。<br />
<br />
　　欧树已经告别旧身份，迎接新生活。外人如果担忧他无法适应这个崭新时代，可以去他寄居的敬老院看看，会发现他并没有像布鲁克斯那样，被街头疾驰的汽车惊扰，一把年纪了还要去超市打工，因为动作迟缓而遭受顾客和老板的白眼。中国的年GDP和财政收入在高速增长，有能力让欧树这样的老人只需呆在房间里休养生息，所以他没有理由像布鲁克斯那样说“我不喜欢这里，我决定――离开”，然后悬梁自尽。<br />
<br />
　　实际上，欧树已经老得无法“离开”。7月22日我们第二次探望他时，他比两周前老了很多，工作人员扶他到院子里散步，他几乎站不直，坏了松紧带的裤子几次滑落到脚面。我们把翻拍好的那张档案里年轻时的黑白照给他看，他面无表情看着，左眼却渗出一滴浑浊的泪珠。<br />
<br />
　　这天是他住进敬老院后唯一一次走出小屋，其他时间全在那个泥墙和木棉瓦搭建的屋子里度过，包括吃饭、洗脸、擦身子、大小便、想心事等等他所有能做的事情。<br />
<br />
　　每天早上6点半，他不再早起散步，而是躺在床上，睁眼看着房顶。想抽烟了，他就慢慢挪到门槛前，坐在地上，举着烟，对着院子里喊：“火！火！”<br />
<br />
　　他的老家黄旗厂村就在4公里之外，但他从没提出去老家走走，倒是他八十多岁的姐夫、堂姐带领一帮亲戚来看过两次。姐夫马支说，欧树父亲劳改十多年后就释放回家了，直到1990年去世；欧树1960年转入云南省二监以后就失去联系，后来大家都以为他死了，每年清明节会给他烧些纸钱。<br />
<br />
　　村干部给他介绍每个亲戚时，他只是笑笑，不说话，眼泪在眼眶里打转。他只记得自己和村庄的名字，小名里有个“福”字。亲戚们哭了，他也跟着掉泪。<br />
<br />
　　这是欧树重获自由后与家乡仅有的两次接触。敬老院其他5个老人从不进他的屋子和他聊天，也许嫌这里太脏。欧树来后头半个月还能吃些东西，敬老院变着花样给他做米线、饵丝、米饭、稀饭，但后来他就很少吃，而且好几次把大小便拉在床上，把敬老院院长戴学义吓着了，连忙跑去镇政府汇报，镇领导也没有好办法，只吩咐叫他每天作好记录。<br />
<br />
　　戴学义说，欧树是建院20年来最费心的老人，他和另一名男性工作人员都50多岁了，照顾欧树显得很吃力，尤其是面对一大堆的脏床单、衣服，想花100元请妇女来洗都无人愿意。<br />
<br />
　　这个小小的乡村敬老院，有自己的猪栏、鱼塘和菜地，全靠自己种养，去年的开支仅1万多元，但戴学义表示，这不是不送欧树去医院的理由。他懂些医疗常识，觉得欧树没有生病，只是老年痴呆。由于多日只靠米汤为食，他担心欧树将不久于人世，等不到春节搬进敬老院新大楼的那天。<br />
<br />
　　7月23日上午，戴学义去黄旗厂村找到村干部和欧树的亲戚们。大家对他热情相待，称赞他“菩萨心肠”，对欧树的照料“比农村里很多人对亲生父母还要好”。但当他提出，希望村里能有人去帮忙洗洗脏衣物时，村干部微笑而坚决地说：“不可能。”欧树的堂侄媳说，她天天忙着要烘烤烟叶，实在脱不开身。<br />
<br />
　　欧树父亲去世后，欧树的大堂侄最后继承了他家的草房，几年前用在外做破烂生意赚的钱，在上面建起一栋3层小洋房，在村里显得鹤立鸡群。我们问，是否考虑过接欧树回村里走走？二堂侄沉默片刻后说：“他一样都不认得了……。”欧树85岁的堂姐则挥手摇头――“麻烦！”<br />
<br />
　　他们强调对欧树父亲当年的亲情：他释放回家后给生产队喂猪，后来眼睛和腿都坏了，主要是这两个堂侄长年照顾，直至去世。现在再冒出一个“死了多年”的人，还需要他们照顾，他们觉得已仁至义尽。欧树姐夫马支说：“政府要么早年放了他，要么就一直关到老死，现在把他放出来，对大家、对社会都是个累赘。”<br />
<br />
　　欧树释放后遭遇的亲情冷遇，和健康状况的持续恶化，让远在官渡监狱的原责任警察王桂春感到吃惊和难受。他否认在欧树77岁高龄后释放是监狱“甩包袱”，而是他的刑期到了，就必然要出狱。<br />
<br />
　　“他获释时身体还是不错的，而且确实想回家，但出去后一切都变了，对一名老人来说，适应起来比较痛苦。更可能出去后，失去监狱里原有的精神支撑。”王警官仔细询问后，这样分析欧树的变化。<br />
<br />
　　“那就是，监狱里他遵守监规，受到尊重，作息规律，有独立人格，对自由和家乡的想念让他觉得还有盼头。但出去后这个念想就没了，不知道活着该干什么，应该怎样与别人打交道。他本来特别爱抽烟，但如果现在都不接你的烟，说明他把心门关上了。他一直是个倔强的人。”<br />
<br />
　　近两年，四川、江西等地监狱相继提前释放大批老病残犯，引起了社会广泛关注，褒贬不一。主流意见称，这批犯人由于政策限制滞留监狱，加剧了监狱医疗、警力资源的紧张，应该让他们在通过人身风险评估后早日回归社会。云南省监狱管理局也在进行同样努力，尚未得到上级答复。<br />
<br />
　　反对声音里，有两种互为交锋：一种认为，就应该让他们在监狱里品尝失去自由的恐惧，以对犯罪形成震慑；另一种认为，让习惯了监狱生活的人回到社会，那才是真正的恐惧。<br />
<br />
　　由于欧树不能言说，无法得知他从一个渴望自由却又习惯体制化生活的囚犯，到获得自由却反被自由困在床头的糟老头，他的内心究竟倾向何方。也许他真的老到无法辨别，任由时代将他推波助流。<br />
<br />
　　欧树那个蓝色旅行包里，有一张纸上写道：“梦缘：你说要来看我的吗？我每天都在判（盼）忘你的到来，每次想到你，心都是那么疼。你过得还好吗？我真的好想你啊！你让我疼的是撕心裂肺。二十几年了，今天才明白想念的意思。想念会让一个人情不自禁的流眼泪。”<br />
<br />
　　不是欧树的笔迹，但欧树在后面写上了自己的名字。旁边是一块泪（水）渍。<br />
<br />
　　我们想念给他听，他轻轻摇头，苍蝇从他长满老年斑的头顶飞起。一切都是过眼云烟。他现在是一名77岁的垂死老人，躺在家乡和自由的边缘，等待死神的敲门。</div>

							
							
							
							
