													<h2>拟建国际化都市 西安要求半数市民会说英文懂普希金</h2>
						
						
																											<div id="postmessage_698358" class="t_msgfont"><img src="http://news.xinhuanet.com/local/2010-06/22/12246805_11n.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　从今年起，西安计划用10年时间初步建成国际化大都市，城市人口达1000万以上。国际化大都市应有什么样的市民素质？西安市文明办、市社科院近日联合发布的《关于加快西安国际化大都市市民人文素养培育对策研究》（下文简称“对策”）提出了基本标准。<br />
<br />
　　今年5月初，西安市文明办、市社科院联合组建了“国际化大都市市民人文素养培育”课题组，用了一个多月的时间，在雁塔、碑林、莲湖等区，与各阶层市民进行沟通，进行集体座谈和个案访谈，并参照国内外国际化大都市经验，最后形成“对策”，提供给西安市委、市政府作决策参考。<br />
<br />
　　课题组组长、西安市文明办主任杨毅波说：“市民的人文素养是城市的软实力，是西安的核心竞争力之一。培育西安市民的人文素养，能够提升西安的知名度和美誉度，能够扩大西安的国际影响力，从而加快西安建设国际化大都市步伐。”<br />
<br />
　　课题组副组长、西安市社科院研究员张永春称：“这个课题中包含很多具体措施，例如到2015年，<strong>一半市民能认500个繁体汉字和会说900句英语，既要诵读唐诗、宋词，也要诵读歌德、普希金，要弘扬西安伟大的城市精神。</strong>”<br />
<br />
　　对策内容<br />
<br />
　　市民英语 八成市民能介绍景点<br />
<br />
　　实施外语口语培训、再教育经费补偿制度。提高市民英语口语水平，使80%的普通市民能对外国游客进行简单景点介绍。<br />
<br />
　　广泛开展认识、能说、会写500个繁体汉字和900个英语词句活动，争取一半市民达到要求。经常性开展由市民、外籍人员参加的多层次、<strong>主题鲜明的诵读汉赋、唐诗、宋词和歌德、普希金等中外经典作品活动。</strong><br />
<br />
　　城市卫生 乱丢垃圾要罚50元<br />
<br />
　　完善城市卫生管理处罚条例，加大处罚力度。许多内地人到香港后都能遵守香港公共卫生规定，原因之一是害怕遭遇高额罚款。西安市文明办、市社科院建议，随地吐痰、乱丢垃圾均罚50元。<br />
<br />
　　要建国际化大都市先治“城市病”<br />
<br />
　　西安市文明办主任杨毅波介绍课题时称，新加坡、东京、纽约、香港、北京、上海等地，在建设国际化大都市过程中，都程度不同地遇到过市民素质不高、管理制度缺失、公共服务设施不完善等共性的“城市病”；西安也无法绕过或回避这些共性问题。<br />
<br />
　　学新加坡 每3人中就有1人是国际人才 新加坡有“总部经济”的美誉，七千多家跨国企业中，60％是设在新加坡的全球或区域总部。在新加坡的就业者中，每3人中就有1人是国际人才。<br />
<br />
　　学东京 禁烟区抽烟一次罚2000日元 东京划定了室外禁烟区，一次违反者将被处以2000日元的罚款，折合人民币160元左右。<br />
<br />
　　学香港 对脏、乱、差要“零容忍”香港对破坏卫生环境者处以高额罚款，培养了香港人对“脏、乱、差”的“零容忍”和严格的城市管理意识。<br />
<br />
　　此外，在主要交通干道和繁华商业区加大垃圾桶的摆放密度和缩短清洁人员打扫距离，增加主要路段的打扫次数。在背街小巷和非交通要道，不但要增加垃圾桶和清洁人员，还要在一些十字路口增加红绿灯设置，做到城内城外一个样，繁华区与非繁华区一个样。<br />
<br />
　　建立分层次的交通违规处罚制度。提高现行行人闯红灯最低罚款额度，由10元提高至100元。对有一定经济能力的人，上调罚款额度。行人按月收入比例（外地旅游人员按处罚的平均额度处理）处罚，车辆按车价比例罚款，最低不得低于行人罚款10倍。凡滞交人员车辆追加滞纳金。所罚金额纳入城市卫生基金。此外，2020年前，西安80%的公交车改为空调车。<br />
<br />
　　户外广告 1/3版面反映西安<br />
<br />
　　西安地区从事户外广告牌、LED、公交车车身广告的公司，必须拿出三分之一的版面和时段，宣传反映西安历史文化的经典诗词、重大历史事件、文明风貌。</div>

							
							
							
							
