核心提示：美国航空航天界的权威杂志《空间与太空技术》周刊报道称，美国情报部门的有关人员向该周刊透露，美国东部时间4月11日下午3时26分，美国监视中国的侦察卫星“长曲棍球”II在经过中国上海空中时，遭到中国反卫星武器“攻击”，失效长达27分钟。<br />
