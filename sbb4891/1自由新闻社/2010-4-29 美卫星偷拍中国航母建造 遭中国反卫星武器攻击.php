凤凰卫视2010年4月28日《军情观察室》节目播出，以下为文字实录：<br />
<br />
美卫星拍解放军航母照被中国“漆雾攻击”致失效<br />
<br />
董嘉耀：根据美国比较权威的军事杂志《空间与太空技术》杂志最新一期，引述来自美国军方情报部门一个最新情报显示，中美之间刚刚在4月份，展开过真正意义上的一个太空的较量。这篇情报文章特别披露说，美国的情报显示在4月11号，美东时间下午3点26分的时候，美国新一代的“长曲棍球”二代的雷达成像新型的间谍电子侦察卫星，飞跃到中国大陆进行例行的拍照，主要是飞到中国的上海的周边，展开侦测拍照的时候，被中国的反卫星武器攻击。<br />
<br />
这篇报道特别提到，当时美军的“长曲棍球”二代的侦测雷达成像卫星，在上海附近希望拍摄传闻当中的，在上海的江南造船厂，中国大陆正在建设的解放军的航空母舰。因为一旦摆在外面的话，根据这个相关的侦测是可以侦测和拍摄到，美军作为情报来收集的。但是就在这个时候遭到了中国反卫星武器的攻击，使到美国的侦查间谍卫星失效长达27分钟。<br />
<br />
