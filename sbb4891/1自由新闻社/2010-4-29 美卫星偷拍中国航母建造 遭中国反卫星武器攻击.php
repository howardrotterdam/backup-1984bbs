<br />
这个弹打开之后，喷出了相关的漆雾，而这种漆雾呢，不是漆也不是雾，实际上是一种磁电的物质，一旦接受到对方的卫星之后，可以覆盖和遮挡吸附对方的镜头还有电子的感应器。使到对方相关侦测的器材完全的失效，但是在若干时间之后，可以在太空挥发掉。不至于使到敌方或者对方的卫星完全的失效，不算是直接攻击的军事行为，属于准军事攻击行为。由于中国大陆的相关喷雾的技术已经成型，而且对美军在上海上空展开侦测的行为做出了喷雾的攻击，使到美军间谍卫星失效27分钟，美国大为的震惊。<br />
<br />
