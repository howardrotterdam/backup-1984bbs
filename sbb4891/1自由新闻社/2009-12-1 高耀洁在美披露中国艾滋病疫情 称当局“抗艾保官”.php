													<h2>高耀洁在美披露中国艾滋病疫情 称当局“抗艾保官”</h2>
						
						
																											<div id="postmessage_350154" class="t_msgfont">高耀洁教授在2009年12月1日世界艾滋日记者招待会发言稿<br />
美国华盛顿全国新闻俱乐部BLOOMBERG 室<br />
2009年12月1日<br />
<br />
女士们，先生们，大家好！<br />
今天我来介绍一下中国艾滋病疫情，我所见所闻的真实情况。<br />
<br />
远在1982年有专家已发现艾滋病病毒进入中国。1984年北京中科院院士曾毅报道了医院血库的存血“被艾滋病病毒污染”；1988年河北省防疫站主任医师孙永德先生发现库血中存有艾滋病病毒之后，他大力呼吁，自河北省卫生厅，河北省委，直至中央卫生部和国务院等有关部门。这些重要举措，负责人民生死的父母官们置若罔闻，更没有采取对艾滋病控制的措施，反而为了致富，大搞“血浆经济”。<br />
<br />
90年代“采血浆站”像春笋一样，仅河南一个省，官办血站200 多个，私办血站多不胜数。全国31个省市，没有一个空白点，更增加了艾滋病病毒的传播与蔓延途径，加上各种离奇的宣传：“卖血致富、有利健康，不得高血压”，欺骗无知的农民蜂拥而上，前往血站卖血，把他们推向死亡之路。这个极为庞大的弱势群体，染上了病程漫长（会拖延十多年）的艾滋病，这些地方的医骗子很多，弄得他们贫病交集。他们又没有文化知识，没有写作能力，又不会说出道理，真可谓有苦难言。此时，官员们又使出几件绝招，对艾滋病属于“保密”范畴。压制媒体对真实情况曝光。河南省为此开除、驱走了十几名记者，阻止外来人进入艾滋村，调查了解情况，或救助艾滋病病人和孤儿，并打压敢说真话的人，等捂盖的措施。<br />
<br />
因此，全国艾滋疫情的真相被封闭了。十年、二十年过去了，迄今中国大陆的艾滋疫情真相仍未能大白于天下，国内外很多人只知道河南上蔡县文楼村有艾滋病病人，其实不然，全国还有很多、很多不被人知的艾滋疫区。<br />
<br />
1996 年4月7日在一次会诊中我发现一个因子宫手术输血感染艾滋病的病人，她输的是血库的血，我敏感的意识到库血感染的艾滋病病毒，不会只有这一个人，她全家特别是她丈夫没有感染艾滋病，令人感到奇怪。此事又遭到省卫生厅官员的训斥，他说“就你们大惊小怪，哪有那么多艾滋病叫你能看见呢？”从此把以往我相信的艾滋病吸毒传染，性乱传播的说法产生了质疑。不久前听说，中国艾滋病的感染途经与国外不同，主要来源于“血浆经济”，而政府官员不敢承担责任，掩掩盖盖，花样百出，阴一套，阳一套，一切为了维护个人和集团的利益，保护自己的乌纱帽为目的，不管小民的死活。同时我开始了解艾滋病传播的情况。<br />
<br />
自1996 年秋，我开始编写印刷预防艾滋病的资料。十多年来共印出120万份，发往全国各地。同时，我又编写了七册有关预防艾滋病的书籍，发往各地50多万册，共出版100多万册。这些费用包刮邮费和外出的旅差费共花去一百多万元人民币。这些费用都是来自各项奖金，稿费和我的私人积蓄。我从未接受过任何捐款。我为何不愿意接收捐款？因为处境险恶。1999年已发现当局对我很不友好。<br />
<br />
2000年对我进行监视，盯梢和跟踪，同时监控我的电话。他们禁止我在大学讲台上作科普讲座，包刮艾滋病知识。<br />
2001年我开始救助艾滋病病人与孤儿，当局便禁止我接见记者，不叫我下乡调查艾滋病疫情，不准许我出国领奖。<br />
2003年，当局利用骗子把我告上法庭，虽然他败诉了，但当局对我仍持反对的态度。<br />
2007 年，美国为我颁发“生命之音”奖，当局阻扰我前往美国领奖，把我软禁在家半个月。由于现任国务卿希拉莉女士的努力，中国放行了。他们马上找我，要我到美国后以高耀洁的名义成立基金会，被我拒绝了。来美国时仍有人监视我，封闭我的消息等，因此把曾金燕（胡佳的妻子）气哭了。<br />
<br />
在美国住了一个多月，很多人挽留我常住美国，他们说：“为了人身安全，不要回去了。”我想本人工作在中国，那里还有很多艾滋病病人和孤儿，他们需要我，我不忍心离开他们。抱着这种态度和目的，我又回到家。当局表面上对我很好，不但送我食品，还送我电器。其实不然，他们在我的住房前后安装了四个监视探头，并且继续监控我的电话，电脑和信件。<br />
<br />
2007年的一天，省政府来了一个大官，他说：“徐书记（河南省委书记）让你写一本《河南艾滋病的今昔》。你可以先去上蔡县文楼村看看……领导说了,我们组织一个写作班子,只用你的带头，指导，签名……”我一听,其中大有文章,我当场拒绝了.三天后他又打个电话,我还是没有答应写这样的书。但我明白了，对我这么好，其原因就在于想利用替他们说假话来骗人。<br />
<br />
2009 年2月，美国国务卿希拉莉访华，要求2月22日见我，当局表面上同意了。2月19日我到达北京，20日派一个已经从中医学院退休多年的党委副书记赵芬莉飞往北京，通过警方去堵截我。她估计我会住在曾金燕家，国保堵住曾家房门，不准任何人出入。其实我未住曾家。折腾了两天她没有找到我。<br />
<br />
2009年3月底，法国驻华大使馆一位女士来电话告诉我说：“法国给你发放妇女人权奖”。我说四月中旬我要去上海领奖，到时我们再说吧。她说好。谁知上海的颁奖会推迟了一个多月。<br />
<br />
5 月6日早上，我的电话又被当局切断。中午有朋友来接我，她说：“你走吧，又要出问题了。”于是中午饭也没吃，就乘汽车到了北京。三天后我去了廊坊市，半个多月后我又去了四川，六月初去了广州，6月12日我住进了广州郊区的明朗村，此处很偏僻。本来我只想住一段时间，和去年躲奥运会一样，两三个月后就可以回家了。同时可以把我手中的三本书稿发给出版社，否则我对不起艾滋病病人和家属，特别是病死的人。<br />
<br />
我进入“防艾、救孤”工作已十三年多！在坎坷的道路上，走啊，走啊。现在走投无路了！官方悬赏500元给举报我的人，（举报其他人赏50元）。他们想尽办法拦阻我们进艾滋村。2003年3月 14日，我和高燕宁教授，杜聪及艾滋病疫区双庙村，差一点被抓住。我往贫困疫区寄东西（书籍、衣物）遭到当局途中没收。生活和行动被限制，电话和电脑被监控，外出被跟踪盯梢，甚至被软禁等，限制我的人身自由，迫使我的工作无法进展。<br />
<br />
6月份，我得知谭作人先生调查四川地震死亡学生的真实人数，他的目的在于帮助弱势群体呼吁，却遭到许多阻力与打击，中国当局以涉嫌“煽动颠覆国家政权罪”逮捕了他。我与谭作人先生的工作性质类似，但我所做的范围广，人数多，时间长，影响大。谭先生的前车之鉴，我不得不提高警惕。我不是贪生怕死，我要把艾滋受害者的真实情况整理成书出版，留给后人，做为历史的一页，否则我死不瞑目。于是我决定外出，这时我不知哭了多少回。我流的泪有时把枕头湿透。我已经80多岁了，在世时日有限，此行明知是埋骨异地。为了中国艾滋病疫情的真相，我实在是不得已而为之。<br />
<br />
近3~4年来,艾滋疫区出现的一些情况,结合本人的遭遇,我明白了,艾滋疫情在农村严峻,血站已转入地下。如今湖北省十堰市郧县孙家湾设“单采血浆站”，组织山区上万名妇女前往卖血，每600毫升换168元人民币（见2009年11月4日《中国青年报》）。还有不少未揭露出来的血站。只要没人说,外面不知道,官员们才能发大财，也能保住乌纱帽,总结他们用的五种妙法:<br />
<br />
1金钱收买，送红包、困难救济、受灾赔偿等。<br />
2物质诱惑，<br />
赠送食品、用物、家具、电器甚至楼房、汽车等。<br />
3荣誉拉拢，奖励、晋升、提拔、党票等。<br />
4给脸色看，压制、打击、恐赫、监控、软禁，甚至造谣毁谤等。<br />
5最后一招，，对以上各种方法不能制服者，编造个莫须有的罪名，劳动教养，刑事拘留，甚至关进监狱等。<br />
<br />
这五种方法很灵，不少敢说真话的人在诱惑，恐吓中倒下来了，有的人再不说话了，有些“能人”一反常态，唱起了赞歌，好一个“太平盛世歌舞升平”来掩人耳目。艾滋病防冶得力，现在局限的“血祸”疫情已过去了。今后艾滋病以吸毒传播，性传播为主要的传播途径，卖血发财的官员们，政绩显赫！达到了维护个人和小集团的利益之目的，管他小民的死活。<br />
<br />
几十年过去了，没有一个官员对“血祸“负责，假如有关人员不为钱权；假如有关部门不这么麻木不仁；假如有一点民生意识；假如不想尽方法捂盖子，艾滋病不会泛滥到这个地步吧？<br />
<br />
对艾滋病传播途径上的看法不同是学术上的争论，竟被官方看成异己人士。救助艾滋病病人是一件好事，在物质上接济，精神上支持，为了他们更好地生存。<br />
<br />
高耀洁2009年12月1日<br />
<br />
