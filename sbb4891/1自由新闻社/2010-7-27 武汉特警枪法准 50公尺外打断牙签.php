中国多个省市近来发生特警狙击手击毙歹徒事件，武汉却从未听闻，他们的神枪手们，为何5年来在实战中没放一枪？据报导，因为太准了，所以没击毙人。<br />
<br />
武汉特警突击大队狙击中队中队长赵晖告诉荆楚网，「狙击一枪毙命只是下下策，万不得已不会用。」<br />
<br />
狙击手是子弹喂出来的，赵晖每年要打3000发子弹，接触狙击枪11年，他打坏5支枪。然而，狙击手并不是一训练就射击，而是常瞄空靶，趴地上就是一上午，纹丝不动。<br />
<br />
赵晖说，要能在远距离一枪命中，影响因素太多，甚至连温度、湿度发生变化，都要校枪。但对于高手来说，枪手自身状态才是最致命因素，因此，他们在训练时每打一颗子弹都要记日记，不仅包括风速，还包括当时的心情、身体状态对准度的影响。一旦状态不好，出现气息不匀，就要立即调整，狙击手平常都靠跳绳来训练气息。<br />
<br />
他说，突击大队共12名狙击手，个个都能在50公尺外打断牙签。<br />
<br />
被问及训练之余，是不是喜欢玩CS（反恐精英）、看枪战片？赵晖摇摇头说，真正拿了狙击枪后，才觉得CS那类游戏很幼稚。 「现在游戏、电影上的狙击枪瞄准镜里都显示十字架，但实际上中国狙击枪的瞄准镜里是6个箭头。」<br />
<br />
赵晖说，狙击手的生活是枯燥的。大多数时间，他们都一个人闷着趴在地上，数小时不能动。整个队里12名狙击手，个个性格喜静不喜动，从舞厅门口走过都很反感，他们只听轻音乐，「我只听刘德华和王菲的歌。」</div>

							
							
							
							
