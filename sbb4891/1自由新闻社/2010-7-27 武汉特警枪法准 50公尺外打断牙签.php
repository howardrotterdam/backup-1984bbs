各位有兴趣的，可以去下载一个Long Range Shooting Simulation来玩玩。。。玩的时候手边准备一计算器先。。。<br />
美国佬的狙击手从测距到测湿度啥的都电脑化了，观测员手腕上有一小电脑，输入各种数据，公式直接算出报数字，射手调好直接开枪就好。<br />
<br />
下面这篇文章有点意思。。。。有兴趣的看看哦。<br />
<a href="http://xcqct.blog.hexun.com/48468460_d.html" target="_blank">http://xcqct.blog.hexun.com/48468460_d.html</a><br />
阅读前提示：本文是写给刚入行的新军迷看的基础知识，而且写得极为罗嗦，已经懂的老同志就不要浪费时间来看了。<br />
<br />
最近比较流行的热门话题是88狙（时髦的称呼是“88阻”，而且还不算错别字）的精度究竟是不是个杯具，在讨论88阻能否打中牙签的问题上，有人深信不疑，而且还晓以民族大义――质疑的都是汉奸、国军、美帝的走狗。但有人认为是假的，绝不可能做到。<br />
<br />
《装备与生存》的另一位主编曾说了一句意味深长的话：“只要有耐心，总是能打到的”。而我也曾在一些论坛上说过：“你给我一把54、足够的子弹和无限的时间，100米打牙签最终总能打中的，不过一把枪可能不够，多给几把，打废了就换吧。”<br />
<br />
上面这句话是在表明，“打得中”和“保证打中”是两个概念。不能正确理解这句话的人，请去请教您的语文老师。<br />
<br />
在外界条件恒定不变的情况下，同一个射手重复射击数次，即使武器、弹药、瞄准点都相同，弹着点也是互不重合的，而是呈现出一种看起来似乎是杂乱无章的分布。这种现象称为弹道的自然散布。正是因为有自然散布的存在，所以射手的水平无论有多高超，瞄准射击时完全没有任何失误，但只要稍为有点距离的话，子弹也不可能永远打在同一个点上（为什么说要“有距离”呢？如果您把枪贴在靶子上，连续打到一个点上也不太难）。<br />
<br />
<br />
射弹散布的典型情况，我们可以看到靠近中心的一半是比较密集的，外围的一半则比较松散<br />
<br />
因此，一个神枪手，即使他瞄准射击时没有出现任何失误，也只能让射弹控制在一个范围内，而距离越远，这个范围就越大。那么假设某把枪配某种弹，在100米距离上的自然散布是直径1cm的圆，在500米距离上是5cm，在1000米距离上是10cm；一个神射手在没有失误的情况下，在100米距离上能保证打中功夫茶用的小茶杯，但在500米距离上就只能保证打中正常吃饭用的普通饭碗，而在1000米距离上就只能保证打中汤碗了。不过随距离而增大的散布面是不能用三角函数来计算的，事实上它是呈喇叭形的。比如在100米散布为1cm，在500米有可能已经是6cm，而在1000米则可能增加到20cm了。总之距离越远，散布面就会越大。假如要用一支只能在100米保证打中乒乓球拍的狙击步枪去打中500米距离上的1枝牙签，如果说“能打中”是没有语法错误的，但如果说“保证打中”那就是吹牛了。<br />
<br />
<br />
沿射向散布图形<br />
<br />
不过有人会说：如果是神枪手，技术达到极致就可以超水平发挥，突破器材的限制。<br />
<br />
那是瞎扯，只有那些有预知能力的超能人，才有可能突破器材的限制。<br />
<br />
一条在地球上大气环境下的弹道是由初速、发射角和弹道系数这三个弹道要素决定的，因此，当我们排除了人的因素（射手的水平）和环境因素（风、气温气压等条件）后，凡是影响初速、发射角和弹道系数变化的因素都会影响自然散布的大小。这些因素都集中在枪和弹的微弱差别上，虽然可能通过选择高精密的枪和弹来减少这些差别，从而降低这些负面因素的影响，减小自然散布，提高命中率，但却无法完全消除（这就跟初中物理所学的“世界上没有绝对纯圆”是一个道理），所以射手虽然可以掌握散布面的规律，却无法掌握子弹会打中散布面中的哪一个位置上。<br />
<br />
那么先来说说与弹有关的因素吧，射手虽然是同一人，枪也可以是同一把，但打出的子弹却不可能是同一发，因此，涉及弹的因素都可以总结为一个“一致性”的问题。我再提醒大家一次，没有绝对的一致性，所谓的比赛弹、高精度弹，其实就是在“一致性”方面比普通子弹更为接近。这里主要有：<br />
<br />
1.&nbsp; &nbsp;&nbsp; &nbsp;点火压力：底火的点火药量、压药密度都会影响击发瞬间时的点火压力，从而影响初速的一致性。<br />
<br />
2.&nbsp; &nbsp;&nbsp; &nbsp;装药量：每发弹的发射药量都不可能绝对完全一致的，无论是相差0.1毫克还是0.001毫克都会导致推力大小产生区别，从而影响初速的一致性。<br />
<br />
3.&nbsp; &nbsp;&nbsp; &nbsp;拔弹力：这取决于装配枪弹时的装入深度和弹壳口中部与弹头圆柱部的紧口力，所以弹头外形尺寸及弹壳口部的微弱差别以及装入深度的微弱差别都会引起初速的不同。<br />
<br />
4.&nbsp; &nbsp;&nbsp; &nbsp;药温：药温变化会通过发射药燃烧速度的变化影响膛压与初速的一致性。同样道理，射击时的枪管温度也影响初速，所以冷枪管和热枪管的初速也不同。这也是为什么狙击步枪的精度测试大多是打3发一组，而突击步枪的精度测试大多是打10发或10发以上一组的原因。<br />
<br />
5.&nbsp; &nbsp;&nbsp; &nbsp;药室容积：这是指弹头装入弹壳后，弹后空间的总容积，药室容积的大小影响了装填密度，装填密度又影响了燃烧时的速度，从而影响初速的一致性，而弹壳内的尺寸公差与弹头装入深度又影响了药室容积。<br />
<br />
6.&nbsp; &nbsp;&nbsp; &nbsp;装药状态：指枪弹入膛后射手发射时，发射药在弹壳内的分布状态。因为弹壳内是有自由空间的，发射药并非把药室完全塞得满满的，因此由于射手持枪发射时做的各种动作以及推弹进膛时的动作都会使装药呈现不同的分布，例如有时集中于弹壳底部，有时是集中于弹头底部，有时是均匀分布在弹壳下侧。这些不同的分布都会引起点火、燃烧状态的不一致，从而影响了初速的一致性。<br />
<br />
7.&nbsp; &nbsp;&nbsp; &nbsp;发射药燃烧的一致性：由于发射药成份的不均匀或药粒形状、药粒尺寸的不一致，尤其是燃烧层厚度的不一致，导致燃烧时的不一致，从而影响初速的一致性。<br />
<br />
8.&nbsp; &nbsp;&nbsp; &nbsp;弹头重量：即使在完全一致的推力下，弹头重量的微小差别也会引起初速的不同。此外在飞行过程中，弹头的重量差异也会导致环境（如风）对弹头的干扰大小发生差异。（所以狙击弹、比赛弹大多是重型弹头，而远距离狙击也经常使用弹头更重的大口径步枪，这都可以减少风偏影响）<br />
<br />
9.&nbsp; &nbsp;&nbsp; &nbsp;弹头质心位置：质心位置的不同，会引起飞行姿态、自转情况和抵御风力的不同，这些都会引起弹道系数的变化。为什么比赛弹大多数采用纯铅弹芯，就是因为越简单的结构越容易保持重心位置的一致性。如果用复合弹芯，因为里面有几个部件组成，质心位置的一致性必然会差一点。<br />
<br />
10.&nbsp; &nbsp;弹头的气动外形：当然了，前面不是说过没有绝对纯圆吗？即使同一个模具出来都会有极轻微的差异，在空气中飞行时，几何尺寸的不标准会引起阻力系数的变化。<br />
<br />
11.&nbsp; &nbsp;弹头断面密度：包括重量和直径，这也是会影响弹道系数的因素。<br />
<br />
12.&nbsp; &nbsp;飞行姿态：章动角越大，对弹道系数影响越大。<br />
<br />
13.&nbsp; &nbsp;飞行速度：与飞行速度有关的因素多了，这里不一一细说（偷懒）。<br />
<br />
<br />
在肉眼看起来这颗子弹的外形很均称，而且弹头位置也很正确，但事实上外形一定有不对称的地方，而且弹头一定是装歪的（请用“世界上没有绝对纯圆”，“两条平行线一定会有相交的一点”这些初中知识去理解）<br />
<br />
<br />
结构越简单的弹头，其质心位置就越容易保持一致<br />
<br />
<br />
由于弹头一定会歪的，因此在膛内运动时，弹头质心就已经不会处在枪膛轴线上。<br />
<br />
<br />
弹头的章动与进动<br />
<br />
<br />
飞行中的激波阻力，如果弹头外形不均称（事实上一定是不均称，只不过肉眼不可辩），就会对飞行的方向产生影响<br />
<br />
<br />
空气动力对弹头的作用，所以说，速度、外形和质心的一致性影响很大啊<br />
<br />
 <br />
<br />
通过上面列出的因素，我们就会发现，现在有些报道里说到有些88阻射手采用“称子弹”的方法，其实没有实际效果，只能起到心理安慰的作用，因为你能称出来的只是整颗弹的重量，当两颗弹的重量完全一致时（假设“绝对”是成立的），但也有可能这颗弹的弹头重一点点，发射药量少一点点，另一颗弹则相反。而且我们别忘了，弹壳重量也是存在着差异呢，何况还有重心位置、紧口力等等的不同，因此称子弹的方式是挑不出各方面的一致性都很接近的弹药来。<br />
<br />
另外某些电影或小说里挫弹头的传说也是一样没有实际意义，在没有精密的量具和器械的条件下，靠人眼和人手去挫，只会越挫越歪。国外有条件的资深玩家，玩手装弹是通过精确的分析天平、拆弹、压装、紧口以及打磨抛光等成套工艺和设备来组装一致性程度很高的弹药来。但是他们还是得买名牌厂家生产的高精密弹头，因为弹头重量、重心、尺寸等方面的一致性还是得靠生产线上出来的高质量产品。<br />
<br />
 <br />
<br />
<br />
第二期《装备与生存》里就有外国玩家投稿关于高精密手装弹的做法<br />
<br />
 <br />
弹药引起的差异有多大？这组图分别表示在M16A2上打M855弹与M4上打Mk262弹在不同距离上的散布情况。即使是短枪管的M4，当打高精度的狙击弹时，比长枪管的M16A2配普通弹打得更准。<br />
<br />
现在说说与枪有关的因素了。<br />
<br />
大家都知道，狙击枪通常比普通步枪打得更准，但通常其生产成本也比普通步枪要贵。当然，一方面这是因为狙击枪的产量本身就比较低，但狙击枪通常生产得比普通步枪精密，这导致它成本高的另一个主要原因。而即使是普通的突击步枪，如果给它换上一根高精密的枪管，它的射击精度也能够显著地提高。而涉及枪械的主要因素有：<br />
<br />
1.&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;瞄准装置：如果瞄准装置不可靠或安装不牢固，射手在瞄准时就会发生严重的误差，而且会由于每打一枪产生的震动导致瞄准装置偏移，即使只是肉眼不可辩的轻微移动，当射程距离达到一种的距离时，也会产生严重的影响。比如假设在50米距离上产生的瞄准误差是1厘米，在100米距离上的瞄准误差就是2厘米，在1000米距离上的就是2米了。三角函数不用我再教一遍了吧？（所以，望远式瞄准镜不能减少枪和弹引起的自然散布，但可以减少瞄准误差，让射手瞄得更准。这就是为什么高精度射击时经常使用望远式瞄准镜的原因。）<br />
<br />
2.&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;扳机力过大或扳机动作生硬：这很容易导致射手在扣压扳机时用力过大或在最后阶段用力不均匀，从而在击发瞬间导致枪身的震动，在弹头出膛前就发生干扰。这些干扰在弹头出膛瞬间产生的影响可能是微不足道的，但当弹头飞到较远距离上时就变得比较严重了，正所谓“差之毫厘失之千里”。这也是为什么现代大多数狙击步枪都采用特别轻的扳机的原因。此外还有些狙击枪或高精度的比赛步枪采用设定式双扳机，看过《公敌》的人可能会记得，那个FBI特工普维斯所用的步枪在射击前先扣下一个扳机，然后再扣另一个。前一个动作就是让主扳机处于将要击发的位置，这样在最后的射击动作时，主扳机的扣力和行程都会特别小。<br />
<br />
3.&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;枪身组件与枪托装配不良，贴合不紧或局部接触，这会导致击发瞬间产生的震动对弹头发生初始扰动。<br />
<br />
4.&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;动作部件产生的振动：这正是许多高精度步枪采用手动方式的原因之一，就是为了减少在扣下扳机后、弹头离开枪膛前，因为自动装填机械的动作所产生的振动影响射击精度。不过随着技术的提高，现代的自动装填技术对射击精度的影响比过去小得多了，所以现代狙击步枪之中也出现了许多半自动步枪，不过现代半自动步枪虽然有能力在近距离打进1MOA，但在远距离上就比较难保持在1MOA内，因此如果是远距离高精度射击，狙击手们还是愿意选择手动步枪。<br />
<br />
5.&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;枪管变形或弯曲：由于枪管制造和装配中内应力的释放，重力的长期作用，太阳照射造成阳面与阴面的热不平衡，使用不慎导致跌落或撞击等等原因，都会导致枪管变形和弯曲，即使只是肉眼不可辩的轻微变形，也会导致对远距离上命中精度的负面影响。<br />
<br />
6.&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;枪管振动：发射时，枪管会由于初始静弯曲（上述原因导致）及多种载荷（枪管上的其他物件）的动力作用，产生随机振动。这些载荷包括：弹头的偏心载荷使枪管发生弯曲振动；弹头的导转载荷，使枪管发生扭转振动；枪管拨动装置载荷产生弯曲振动，高压气体突然充满枪管产生的布尔登效应发生弯曲振动等等。这点也正是现代狙击步枪大多采用浮置式枪管的道理（有些资料译为浮动式枪管），即枪管只与机匣连接，不接触其他物件。所以经常有人说，如果88阻把那个两脚架从枪管移到护木上，会稍为提高射击精度。<br />
<br />
7.&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;枪管温度：前面提到药温时说到枪管也会影响初速的一致性，现代狙击步枪大多数采用比较厚的重型枪管就是因为管壁厚的枪管的热容量较高，无论少量射击还是太阳暴晒的影响都比薄枪管要小。有些狙击枪在枪管上拉一条布条，其实就是充当枪管的太阳伞，减少因暴晒产生的枪管发热现象，同时也可以减少因为枪管发热后的上升热气使瞄准镜产生虚像。<br />
<br />
8.&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;膛口流场干扰：枪弹发射时，其实先出膛的并不是弹头，而是弹头前面的空气以及从弹头壳与枪管壁之间微小缝隙泄出的少量火药气体，这些气体在喷出枪口后产生的流场会对弹头产生初始扰动。如果膛口流场不对称，就会产生负面影响。导致膛口流场不对称的原因可能是：枪口切面与枪膛轴线不垂直或有磨损或损坏，枪口装置的不对称（例如某些枪口防跳器），枪口附件的影响（如刺刀），枪口过于贴近地面、墙壁或其他物体（所以狙击手在选择射击阵地时就要注意这点）。顺便提一下，有时消声器并不一定会增加散布面，对于某些突击步枪（安装防跳器）来说，消声器反而能减少散布面。<br />
<br />
9.&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;枪管内膛有锈蚀、污垢及擦拭不良：所以狙击手和比赛选手一定要勤加保养枪支。基于同样的原因，虽然现代军用枪支大多采用内膛镀铬的方式来提高耐用性和战场上的可靠性，但即使是军用狙击枪也很少在内膛镀铬，因为铬层的不均匀或脱落都会对精度产生影响。而国外一些有条件的厂家则采用不锈钢枪管来提高耐用性，但国内的不锈钢材料还没有达到适合做枪管的水平。<br />
<br />
10.&nbsp; &nbsp;&nbsp;&nbsp;枪管内膛的磨损严重：对于狙击步枪来说，枪管的精度寿命比突击步枪要短，这是因为狙击步枪对精度要求较高，而枪管是必定会磨损的。当枪管的磨损达到一定程度时，对于突击步枪来说，也许精度还是足够的，但对于狙击步枪来说，可能已经是不合格了。因此一个狙击手如果始终只使用一把枪的话，那么这把枪在他从一个普通射手成长为狙击手之前可能已经换过好几次枪管了，因为枪法是要靠子弹喂出来的。<br />
<br />
 <br />
<br />
　<br />
<br />
<br />
装上刺刀时的膛口流场――在《兄弟连》里有一段情节提到装了刺刀就打不准，有观众用重心影响来解释，其实M1伽兰德本身就是重心极为靠前的武器，挂个刺刀影响对重心影响不太大，主要原因还是从非对称膛口流场来解释。<br />
<br />
<br />
枪口太贴近地面时，地面对膛口流场产生的影响。所以射击时枪口不能太贴近地面，否则子弹会向上飘。<br />
<br />
　<br />
除此以外，当然还有风、温度、湿度、气压等等外在的环境因素。那么除了这些，还有没有其他原因呢？当然还有，不过请正确理解我的标题“主要因素”，即使是上面提到的那些主要因素，我也只是简单地介绍了一下表面现象，而且因为嫌麻烦和偷懒，在上面提到的那些主要因素中，我还只是挑出容易一句话说清楚的内容来解释，太复杂的都难得表述了，所以还有许多更细致的原因和现象都没有细说。有兴趣详细了解的读者可以去买兵器工业出版社的《射击与命中的科学》和《步兵武器射击学理》来看看，限于篇幅，我这里不可能把两本书七百多页的内容全解释清楚。<br />
<br />
<br />
<br />
　<br />
<br />
总而言之，有许多影响射击命中的因素是不可以通过人的意志去扭转它、绝对地控制它的，拥有好的射击技术，可以打出接近枪和弹的极限――但不可能靠人的意志超出水平，除非是运气。比如你用一支只能在100米保证打中乒乓球拍的狙击步枪去打500米时，恰好子弹“偏”到了牙签的位置上，一枪就命中了，这并不表示你的枪法超越了器材的局限，只是运气好而矣，但要想每一枪都有这样的运气就难了。其实在国内某兵工厂就发生过一个真实的笑话：某个出名枪法臭的靶工，有一天正要验收一把54式手枪，他随手抬起手枪打出了第一枪，枪响靶落――刚好打断了靶绳。六合彩这么难中都有人能中，但不可能有人天天都中奖。<br />
<br />
当然不是说射击技术就没有用了，技术好的话，起码瞄准误差会减少，能把瞄准点对准散布面的中心位置，而且在射击中发现弹着点的偏差异常，就该知道进行什么样的修正了。<br />
<br />
<br />
散布面与目标大小的关系，当散布面小于目标时，瞄准没有错误的话就能保证打中（百发百中），但如果散布面大于目标时，则只能是有机会打中。所以“能打中 ≠ 百发百中”<br />
<br />
<br />
在散布面大于目标的情况下，如果把目标完全套进散布面内，起码还是有机会打中目标的，但如果散布面偏出目标以外，那么就很难打中了。所以射击技术还是很重要的。<br />
<br />
　<br />
<br />
附注1：几种国产武器的射表选录（取自解放军出版社的《轻武器射击实用手册》2000年版）。按照军标，射表数据是按普通射手在正常天气条件下射击获得的测量数据，不过事实上真正的定装数据通常是由优秀射手打出来，以尽量获得较好的性能表现来说服军方。不过即使如此，射表数据仍然是一个平均值，假如精挑细选出最好的枪和最好的弹来，再让最好的射手来打，是能够打出优于射表的成绩来。但要在100米打中牙签还是得靠些运气。<br />
