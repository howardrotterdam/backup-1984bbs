<br />
附注2：MOA，即Minute of Angle，翻译成中文就是分角（这是美军的6400密位，而不是解放军的6000密位），其实是全数散布圆的直径。1 MOA表示在100码距离上直径为1英寸的圆，500码距离上直径为5英寸的圆；而0.5 MOA则表示在100码是半英寸、500码是2.5英寸的圆。至于分别表示在100米是多少？1000米是多少？不用我再教三角函数了吧。<br />
<br />
附注3：MOA其实不是最好的精度界定方式。比如NRA的测试报告通常都是打5发5组再取平均值，比如美军的标准军规中其实是用AMR（Average Mean Radius平均半径），即打完n发后，先找到一个平均弹着中心点，然后量出来各个弹孔到这点的距离再平均一下。而我军的军规同样是要靠n发后进行量度和计算。在我前面提到的两本书里也详细讲解了方法和公式，不过当年我的高数补考了两遍才被老师放水过去，而且出来工作十几年早已经忘得一干二净了。连我自己都百分百理解的东西，抄在此波纹上估计只会让更多的人头晕而矣。至于MOA只是打出n发后直接量离得最远的两个洞中心距离，许多民间射手是打3发，也有打5发。不过对于大多数玩枪的人来说，MOA表示法不需要运用统计学原理，虽然不稳定，但方法简单，不用动脑子。比如FNMI的M16A2的出厂验收只是100码10发散布2英寸（直径5.08cm的圆）就算合格，这样验枪的工人只要打10枪再量一量最远的两个弹洞之间的距离就知道是不是合格，不需要每次都进行细致的测量和计算。</div>

							
							
							
							
