													<h2>因泄露与金正日会谈内容 胡锦涛翻译被秘密处决</h2>
						
						
																											<div id="postmessage_698808" class="t_msgfont">2010-06-23 香港O果日<br />
<br />
“外参”月刊惊爆，胡锦涛的韩文翻译张留成向韩方泄漏胡锦涛与金正日会晤机密被判死刑。图为胡锦涛今年5月与到访的金正日会晤。<br />
<br />
中共内部又爆间谍案。据“外参”月刊昨爆料指，曾任胡锦涛翻译的中共对外联络部（简称中联部）处长张留成，被南韩情报机关收买，充当南韩间谍，出卖胡锦涛与北韩最高领导人金正日会谈机密，在近期遭处决。<br />
<br />
报道指，张留成身为中联部二局（即亚洲局）韩朝处（负责南韩和北韩事务）处长，曾2005年跟随中共总书记胡锦涛出访北韩，以及在北韩领导人金正日在2006年来华访问期间，担任胡的翻译，但他涉嫌向南韩当局出卖胡与金两人会晤的机密内容，令中国和北韩两国顶级机密外泄。<br />
<br />
禁朝鲜族人任敏感职位<br />
<br />
据悉，事件令胡锦涛大为震怒，更引起中共高层对间谍活动渗透到党政中枢而担忧。但因朝鲜半岛问题十分敏感，为免影响与南韩关系，中方惟有秘密处决张留成。事后，中共内部作出决定，今后凡涉及朝鲜半岛敏感部门的核心主管人员，不再安排朝鲜族人士出任。据透露，张与中共政治局委员，国务院副总理张德江一样曾留学北韩。但张的具体身世不详。<br />
<br />
据报道，中国国家安全部早前曾部署一次名为“春晖行动”，专门针对日本，南韩和北韩的反间谍行动，落网者包括中国社科院日本研究所前副所长金熙德，传他充当南韩间谍;另一落网者是社科院韩国研究中心前研究员，国务院朝鲜半岛专家李敦球，他涉向北韩出卖情报。据悉，金，李均属朝鲜族人。<br />
<br />
2007年2月，中国驻南韩大使李滨遭中共当局拘捕。传他因为喜欢喝酒，在酒席间向南韩情报机构泄露国家机密，后来他被开除党籍和公职。2007年11月，中国国际友好联络会理事，解放军大校王庆前因向日本人泄密，被军事法庭判死刑。同年底，新华社外事局原局长虞家复，因涉向美国和南韩情报机构出卖情报，与夫人一起被安全部门逮捕。<br />
<br />
江泽民智囊妻成日目标<br />
<br />
报道指，针对外国情报机构渗透，中共反间谍机构相当紧张。曾是江泽民首席智囊的现任中共书记处书记王沪宁，早年秘密再婚，新娘是小他12岁的复旦大学外交关系博士萧佳灵。婚后，萧到日本东京大学进行博士后研究，原定为期一年，但不到八个月即匆匆回国，原来是国家安全部门获悉，萧在日本已被日本情报机构关注，有可能会被策反，遂报告有关部门，将她紧急召回国。<br />
<br />
“外参”明镜集团属下月刊，创办人是何频。他曾因创办多维新闻和出版多种中共政坛内幕书籍而受外界关注。<br />
<br />
本报记者<br />
<br />
近年中共揭发间谍事件<br />
<br />
&#8226;中联部韩朝处处长，胡锦涛翻译张留成，充当韩国间谍近期遭处死。<br />
<br />
&#8226;社科院日本所前副所长金熙德（图），涉嫌替韩国当间谍，去年初遭拘捕。<br />
<br />
&#8226;中国国际友好联络会理事，军方大校王庆前，向日本人泄露军事机密，07年被判死刑。<br />
<br />
&#8226;新华社外事局原局长虞家复（图），涉嫌向美国及韩国情报机构出卖情报，在07年底被国家安全部间逮捕。<br />
<br />
&#8226;社科院办公厅处长陈辉，将中国对日外交政策文件交日本外交官，05年被判囚13年。<br />
<br />
&#8226;中共解放军少将，空军指挥学院院长刘广智（图），因为替台湾当局出任间谍，在04年被中共处死。<br />
<br />
资料来源：“苹果”资料室<br />
<br />
朝鲜半岛火药库掀间谍战<br />
<br />
中共的东亚问题智囊沦为间谍，给东亚地区近年频发的国际危机增添另类诠释。有专家指，因朝鲜半岛已成为东半球的“火药库”，各国都利用中国刺探情报，日本，南韩等国媒体近年纷纷增派记者，驻中国与北韩邻接的辽宁驻点采访，可见一斑。<br />
中共智囊成收买目标<br />
<br />
专家指，外国情报机构将中共智库研究人员作为收买对象，皆因这些人能接触到不少内部文件，了解机密，通过他们可间接了解中共对东亚局势的决策内幕。境外情报机构大多以“学术交流”名义，向中国智库埋手，这样既便于掩饰，也容易让被收买者上钩。<br />
<br />
此外，北韩也在中国大搞谍战，以30万至50万美元（约233万至389万元）一人的高价，在与中国接壤的延边发展间谍，如近年延边国家安全局三名朝鲜族间谍，包括一名处长，一名科长和一名科员，被北韩间谍重金延揽，至使中国派驻北韩的数十名特工神秘失踪。<br />
三名叛谍最后被中共处决，延边国安局大换血。<br />
<br />
本报记者<br />
<br />
中联部掩饰情报收集工作<br />
<br />
中共中央对外联络部（简称中联部）成立于1951年，是中共中央负责对外工作的职能部门。官方宣称，它的工作性质主要是研究国际形势和重大国际问题发展变化，向中共提供有关情况和对策性建议;处理中共与外国政党，政治组织的交往联络等。<br />
向外国“输出革命”<br />
<br />
过去几十年，中联部一直被视为中共神秘部门，皆因在毛泽东时代，中联部还负有向外国“输出革命”的使命，特别是一些国家的激进共党组织，多透过中联部获得中共精神甚至物质支持，令这些国家政府备感头痛。<br />
<br />
冷战结束后，中共渐放弃输出革命，中联部也渐回归正路，担当中共与外国政党联络的机构。但由于中联部本身负有收集外国政党情报功能，于是成为中共情报机构对外工作的掩护，军方和国安等部门不时会借此平台，开展对外情报收集工作。亦因如此，中联部工作人员往往容易成为外国情报机构青睐的目标。<br />
