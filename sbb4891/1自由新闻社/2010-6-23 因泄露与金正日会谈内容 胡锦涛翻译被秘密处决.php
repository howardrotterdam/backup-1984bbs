<br />
 <br />
&nbsp; &nbsp; 2月19日，香港《明报》援引北京消息，直指本次涉嫌泄密的是金熙德。报道称，有关方面怀疑金收受贿赂，将其掌握的有关朝鲜机密情报透露给韩国情报人员。而内地有关部门在今年1月初对此事展开调查，金熙德案目前已进入司法程序。<br />
<br />
 <br />
&nbsp; &nbsp; “中日关系的第一人”卷入泄密案<br />
<br />
 <br />
&nbsp; &nbsp; 在日本和韩国的相互指责之下，中国方面则对此事表现得相当谨慎。中国社科院在接受境内媒体查询时均称“不知道有这回事”。而金熙德所在的日本研究所也称：“金老师在出差，但不知道何时回来。”而在2月19日举行的外交部例行闻发布会上，有记者问金熙德是否因泄密被捕，姜瑜回答也同样为“不知道此事”。<br />
<br />
 <br />
&nbsp; &nbsp; 在中国社科院里，研究朝鲜半岛局势和日中关系的被公认的第一人是日本研究所对外关系研究室主任，现年55岁的金熙德。金熙德直到2008年还活跃在各大中外媒体，已东北亚问题权威专家的身份讲解和评论该地区的各种问题。但自2009年1月中旬开始，金突然从公众视野消失。<br />
<br />
 <br />
&nbsp; &nbsp; 本刊记者连续数日拨打金熙德的手提电话，但均未能接通，其家庭电话也一直无人接听。一位熟悉金熙德的日本记者称，涉嫌该案的似乎还有他的妻子。金熙德的其他亲属目前在日本，无法取得联系。<br />
<br />
 <br />
&nbsp; &nbsp; 金熙德1954年生于吉林省延吉市。1978年1月，他成为恢复高考后第一批大学生之一，进入延边大学政治系学习。1982年1月，金成为延边大学日本哲学史专业硕士生，后留校任教。1986年，金已访问学者身份赴美国康涅狄格大学政治系学习。3年后，赴东京大学大学院攻读国际政治学博士课程，获博士学位。<br />
<br />
 <br />
&nbsp; &nbsp; 1994年6月，金熙德回到中国，进入中国社会科学院日本研究所。次年，金开始担任中国社科院对外关系研究所主任，主要从事日本外交，中日关系，东北亚局势等领域研究。现任中国社会科学院日本研究所副所长、中国社科院研究生院教授，被国务院授予政府特殊津贴及证书。<br />
<br />
 <br />
&nbsp; &nbsp; 金熙德在国外发表有关日本外交、中日关系以及东北亚问题的论文达百余篇，近年来又以研究日本对华ODA著称。他撰写的文章还曾被刊登中共中央机关报《人民日报》上，这份报纸的言论常常被认为代表着中国官方意志。<br />
<br />
 <br />
&nbsp; &nbsp; 朝鲜情报的争夺<br />
<br />
 <br />
&nbsp; &nbsp; 2008年8月，首尔的情报机构对外透露，朝鲜最高领导人――66岁的金正日在中风后接受了脑外科手术。由于没有明确的继任者，当时这一消息引起了外界对朝鲜面临领到真空的恐慌。国际社会一直在努力停止这个国家的核武器发展计划，朝鲜的情报开始变得炙手可热。<br />
<br />
 <br />
&nbsp; &nbsp; 韩国尤其加大了情报搜集力度。近期。韩国媒体称，朝鲜正着手试射射程更远的“大浦洞2号”导弹，改良型“大浦洞2号”据说射程可以达到美国本土，甚至可能具有搭载核弹头的能力，朝鲜半岛局势因此再掀波澜。<br />
<br />
 <br />
&nbsp; &nbsp; 此前，朝鲜试射的导弹在起飞40秒后坠落到两公里以外的海峡，美日两国对改良后的“大浦洞2号”试射会否发生偏离颇感担忧，遂加强各自的防御和侦察力量。韩国则强烈指责朝鲜的举动严重威胁了东北亚的稳定和韩朝关系。<br />
<br />
 <br />
&nbsp; &nbsp; 2009年是中朝建交60周年，也是中朝友好年。2009年1月底，金正日会见中共中央对外联络部部长王家瑞，这是自金正日被报中风以来首次与外国官员会面。应各国要求，中方在2月上旬，曾召集各大使馆对金正日的健康状况进行说明。<br />
<br />
 <br />
&nbsp; &nbsp; 除金正日的健康问题，与朝鲜前途相关的继承人问题也是各方关注的焦点。在“一人统治”的格局下，领导人病故往往是国内政治的最大危机。按照普遍的猜测，金正日的三个儿子中的一个将接替他的职位，其中主要人选又是金正日的大儿子金正男(38岁)和小儿子金正云(26岁)。<br />
<br />
 <br />
&nbsp; &nbsp; 《朝鲜日报》2009年1月28日报道称，金正日与结束王家瑞同机抵京，媒体在北京机场询问金正男继承问题，他表示：“本人不关心(继承问题)。”而对金正云可能成为接班人的猜测，金正男说：“没有任何信息，请亲自问弟弟。”但他接着的一句补充引发了海外媒体的联想，他说：“王家瑞部长也同机抵达，也可以问他。”此言使媒体推测，王家瑞在访朝期间已与金正日讨论过继承人问题。<br />
<br />
 <br />
&nbsp; &nbsp; 而在中国看来，容易影响朝鲜局势的金正日健康问题和继承者问题是“最敏感的话题”，被禁止谈及。但该名社科院学者是否因触犯这一禁区而被捕还不得而知。<br />
<br />
 <br />
&nbsp; &nbsp; 社科院成泄密高危机构<br />
<br />
 <br />
&nbsp; &nbsp; 中国社科院是中国哲学社会科学研究的最高学术机构和综合研究中心，下辖14个研究单位，也是中央和国务院的“思想库”、“智囊团”，直接为中央决策服务。<br />
<br />
 <br />
&nbsp; &nbsp; 正是由于中国社科院的重要地位和所从事的某些重要研究。近年来，中国社科院成为各国谍报机构所关注的对象。据悉，此次社科院专家泄密事件只是中国社科院近年来众多泄密事件之一。<br />
<br />
 <br />
&nbsp; &nbsp; 2005年5月，中国社会科学院办公厅主任助理兼秘书处处长陈辉被捕。他被控将有关中国对日外交政策的国家机密，卖给一名日本外交官，陈辉因此被判处有期徒刑3年。但涉案的这名日本外交官身份一直未被透露，当时日本大使馆发言人否认知道这起案件，而中国外交部也拒绝就此发表评论。<br />
<br />
 <br />
&nbsp; &nbsp; 陈辉因其在社科院的特殊岗位，一直能接触各种机密文件。如现今这位社科院学者被捕一样，当时在社科院内部，陈辉被调查的消息也被有关方面告知不得传播。在陈辉被捕前一个月，中国社科院公共政策研究中心副主任陆建华也因为泄露国家机密被捕，后陆被判处有期徒刑20年。陈、陆二人是密友，但现在仍无法确认陆建华是否涉及陈辉的案件。和金熙德相似，陆建华也是中国知名研究者，曾任权威年度报告《中国社会蓝皮书》的主编，也经常以“智囊”身份出现在中央电视台、人民网等大陆官方媒体上。<br />
<br />
 <br />
&nbsp; &nbsp; 社科院最近一次卷入涉外泄密案传闻的是韩国研究中心研究院李敦球。据消息源披露，李敦球被指控收受贿赂向朝鲜泄露国家机密。李曾兼任国务院发展研究中心朝鲜半岛研究室主任，也是中国最知名的韩朝问题专家。尽管社科院内部对李敦球案迄今仍然时有议论，但官方无任何消息披露，李的具体内情和下落外界无从知晓。<br />
<br />
 <br />
&nbsp; &nbsp; 不过，包括金熙德在内，这些被传涉嫌泄密者的真实情况或许要靠时间来解密。</div>

							
							
							
							
