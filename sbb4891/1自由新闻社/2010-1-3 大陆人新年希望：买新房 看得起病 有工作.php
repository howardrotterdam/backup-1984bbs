20100103 12:35:27&nbsp; &nbsp;&nbsp; &nbsp;<br />
 <br />
（中央社台北3日电）大陆经济条件改善了，但出现房价飞涨看病难，求职不易窘况。新的一年，新的期待，大陆民众普遍的期盼和梦想是，买得起房、读得起书、看得起病、找到工作。<br />
<br />
2010年新年前夕，新华网记者在福建、江苏等地采访了民众，有学生、公司职员、下岗职工、政府公务员。他们提出新年新期望和梦想是，「买得起房、读得起书、看得起病、找到工作」。<br />
<br />
「现在热播的电视剧『蜗居』中的海萍可能就是以后我们的下场，做一辈子的房奴。我做梦都想着哪一天房价突然跌到我能买得起的程度。」24岁的厦门大学法律硕士陈琴最希望房价能够降下来，「我买不起房子，以后结婚肯定愁死了。」<br />
<br />
福建公务员叶子最期盼的也是房价下降。「我现在和父母住一起，但我要结婚，没有房子怎么行？」叶子说，「我只想买一个80平方公尺的小房就可以了，但福州的平均房价7、8千元1平方公尺，像六一路一些地段都已经上万了。」<br />
<br />
「我的收入还不错，但1个月工资还不够买1平方公尺房子。」叶子说，「除非家里给我极大支持，也只能付个首付（首期贷款），房贷还要自己慢慢还。」<br />
<br />
51岁的厦门民众李新希望在新的一年里，能够让孩子上学的费用降低一些。「9年义务教育孩子没赶上，从小到大读书都要交费。」李新说，养个小孩不容易，单学费就是很大的一笔负担。<br />
<br />
李新有4个孩子，有3个在读大学。「现在大学学费一样要很多钱。」他说，「希望国家能加大教育投入，把学费降低一点，减轻老百姓的教育负担。」<br />
<br />
南京一家从事网吧经营的业主王婷一直为女儿的上学费用犯愁。「我女儿3周岁，上幼儿园，一个月就要1100元，快赶上一个人的工资了。」王婷说，公立的幼儿园便宜一些，但也要6、7百元1个月，而且还要交高额赞助费。<br />
<br />
厦门大学法律硕士林恒说：「一个民族的成长离不开教育，希望在新的一年里，义务教育的范围能够向大学、学前教育扩展，让每个人都能经历上学读书这样一个美好的阶段。」<br />
<br />
47岁的林福强是一位公车司机。他说，他一个月收入只有2000多元，不敢轻易进医院。「去一趟医院可能一个月的工资就没了。」林福强说，最好是不要生病，实在扛不住了就到附近的小诊所打两针。<br />
<br />
厦门曾厝P一家门店的店主林天庆说，他有一次摔倒了去医院，缝了几针就要400多元。「我们是要住院才能报销，小病又不能去住院。」林天庆说。<br />
<br />
除了看病贵，看病难也让许多人头痛。江苏靖江一所学校的老师张凌艳说，她这么多年没上过几次医院，可小孩出生后，跑医院的次数多了。「上大医院虽然放心，但排队实在让人发愁，希望社区医院的软硬件都能加强，让老百姓能放心就近看病。」她说。<br />
<br />
「『看病难、看病贵』多年来一直是老百姓期待解决的难题，新医改方案出台后，给我们带来了盼望已久的曙光。」25岁的厦门大学硕士生段庆菊说，期待在尽可能短的时间内，能够建立功能完善的医疗卫生体系，提升人民群众健康水平。<br />
<br />
「曾经『读书改变命运』是我们为之奋斗的动力，可是今天我们看到的却是大量毕业生找不到工作。」段庆菊说，「我有好几个同学都忙于找工作，不是很顺利，希望就业局面能有所好转。」<br />
<br />
「希望国家为大学生创造尽可能多的就业岗位。」24岁的大学生陈豫晓说，新的一年不要再让毕业生迫于现实压力都去考公务员，不要让毕业生为了毕业后能找到一份工作而在学校浮躁不安。<br />
<br />
46岁的张美凤在厦门开一个小门店。「下岗后卖过早餐、开过水果店，但是都没有赚到钱。」张美凤说，没有文化，年龄又这么大了，想再找份工作很难，希望政府能够对下岗职工再就业有一些补贴。<br />
<br />
「多提供一些就业机会、实习机会，这样我们进入社会的时候就不会面临没有工作经验的尴尬，同时也能更快地和社会接轨。这是我的新年期待。」厦门大学研究生张银洁说。990103</div>

							
							
							
							
