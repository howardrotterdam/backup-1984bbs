													<h2>摇滚英雄演唱会在工体举行</h2>
						
						
																											<div id="postmessage_802173" class="t_msgfont">摇滚英雄演唱会工体怒放 何勇张楚崔健感动全场<br />
<br />
2010年08月28日08:43<br />
新京报&nbsp;&nbsp;康沛<br />
<br />
<img src="http://www.sinaimg.cn/dy/slidenews/4_img/2010_34/703_148196_658415.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
崔健作为演出的压轴登台，以一首《超越那一天》结束了这个夜晚。<br />
<br />
<img src="http://www.sinaimg.cn/dy/slidenews/4_img/2010_34/703_148121_351296.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
张楚的《姐姐》让不少人感动如初。<br />
<br />
<img src="http://www.sinaimg.cn/dy/slidenews/4_img/2010_34/703_148105_434363.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
丁武弹吉他<br />
<br />
<img src="http://www.sinaimg.cn/dy/slidenews/4_img/2010_34/703_148214_606291.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
何勇摇滚激情<br />
<br />
昨晚，“怒放”摇滚演唱会在工人体育场举办。被冠以“英雄”之名的摇滚老炮们在工体场偌大的舞台上，用一人两三首歌的演出找回了当年的辉煌，抱着准备好的感动而来的观众则在台下满意地挥舞着拳头，享受一场音乐飨宴的同时也找回了曾经的青春情怀。<br />
<br />
观众<br />
穿着海魂衫戴着红领巾<br />
<br />
昨晚工体场的上座率达到了几乎满员的程度，门口的黄牛票价居高不下，而从下午五点开始，工体附近的交通拥挤程度比以往的任何一个周五都高。不少观众都穿着带有红五星、红领巾、海魂衫等和上世纪九十年代主流摇滚乐配合的服装元素的服饰。与其他演唱会以女性观众为主的情况不同，来看“怒放”的观众以三十岁以上的男性观众为主，有带着女伴的，也有几位中年好友结伴而来的。<br />
<br />
观众席上的荧光棒密集程度虽然比不上年轻一代歌手的演唱会，但在大屏幕上没有字幕提示的情况下，全场大合唱的默契程度一直都非常高，场地区的观众更是从头站到尾，整场演唱会的热度一直都没有停下。<br />
<br />
曲目<br />
有经典之作也有偏门冷歌<br />
<br />
“怒放”给人的感觉，与其说是一个拼盘演唱会，不如说是一个搬到场馆里的众星云集的摇滚音乐节。由于有十几组艺人（乐队）参演，每组艺人只能有演唱两首（至多三首）歌曲的时间。这样的安排虽然让表演者不一定能够尽兴，但每个人都拿出了自己最“压箱底”的经典作品，观众在短时间内获取的信息密度极高，而台上乐队和歌者的快速上下则带来了一种独特的时光变换感。<br />
<br />
整个舞台的设计可以用“大气”二字形容，横贯舞台左右的大屏幕足以见得制作方的大手笔。整个编排中最值得称道的是乐队的切换速度，上一支乐队表演完毕后，大屏幕上播放一两分钟的短片，下一组表演乐队就能全部就位。<br />
<br />
尽管只有两三首歌的表演时间，但老摇滚们基本都拿出了自己状态最好的一面，瞬间“怒放”。在曲目选择上，有人全部拿自己最经典的作品上阵，郑钧演唱了《回到拉萨》、《灰姑娘》和《赤裸裸》，有人则选择了流行度相对并不那么广的歌曲，如朴树的《傲慢的上校》和许巍的《两天》，齐秦也没有唱《大约在冬季》等经典曲目，而是带着他的“虹”乐队，唱了《痛并快乐着》等两首硬朗风格的歌曲。崔健最后出现在舞台上，他以《超越那一天》结束了整场演出，而这首歌曲并没有引来观众的大合唱。<br />
<br />
直击现场<br />
老炮组 何勇依然犀利，张楚让人落泪<br />
<br />
在演唱那些曾经影响一个时代并久经时间考验的歌曲时，老歌手们表现得大多令人满意，嗓音和表现力并没有随着时间的流逝而减退。何勇穿着标志性的海魂衫，唱着《姑娘漂亮》亮相，曲目间隙他对着台下大喊：“北京的姑娘，你们漂亮吗？”其犀利真是不减当年。后面坐着的依然是他的爸爸，在演唱《钟鼓楼》的时候为他弹奏三弦，时光一刹那穿越回了1994年12月的红|体育馆。另一个比较大规模的合唱出现在张楚出场演唱《姐姐》时，他消瘦飘忽的气质一如当年，当唱到“姐姐，带我回家”时，不少观众眼含热泪。而在唱《蚂蚁蚂蚁》的时候，大家则都跟着鼓点快速鼓掌。<br />
<br />
唐朝丁武的嗓音还是很嘹亮，而压轴的崔健则以一首《不再掩饰》开场，在演唱《超越那一天》的时候，崔健邀请了十几个姑娘和他一起上台跳舞，给这首歌增加了热烈的气氛。<br />
<br />
中生代组 许巍引发大合唱 汪峰敢唱新歌<br />
<br />
许巍、朴树、汪峰和郑钧是中国摇滚乐的中坚力量，也是摇滚乐和市场结合最好的几位大将。昨晚他们的演出也都保持了一贯的商演水准。朴树的造型颇有新意，戴着头巾的他很像是最近热映的电影《敢死队》里一群肌肉男的模样。话不多的他唱了《傲慢的上校》和《生如夏花》两首歌就默然离场。<br />
<br />
汪峰是昨晚的所有歌手中唯一一个有胆唱新歌的，他在《怒放的生命》和《飞得更高》两首百唱不厌的大金曲中穿插了一首去年面世的《春天里》，一样迎来全场合唱。<br />
<br />
许巍的《两天》这首曾收录在《红星一号》合辑里的歌被他首先演唱，不少观众都发出了茫然的疑问“是不是新歌”，不过全场最大的大合唱却出现在他演唱《蓝莲花》这首流行大金曲的时候，无论是在场地区还是看台区，几乎所有观众都站起来一起合唱。<br />
<br />
港台组 黄家强唱Beyond金曲 信浓妆登场<br />
<br />
虽然“怒放”演唱会的主线是回顾北派摇滚，但黄家强、齐秦和信这三个港台歌手的加入则给演唱会带来了不一样的色调。<br />
<br />
黄家强是出场的“港台组”第一个成员，当《光辉岁月》的音乐一响起，所有的观众又一次站起来大合唱，足以见得曾经的Beyond从当年到现在的巨大影响力。<br />
<br />
信则在昨晚做了一个彻底的颠覆，他化着浓妆出场，给这场演唱会带来了视觉冲击和华丽金属摇滚的气息。虽然信的造型改变让观众议论纷纷，但在唱《死了都要爱》之前的发言却体现出了他的几分情怀：“这首歌其实是我不太想唱的歌，我以后不想唱这种芭乐歌，希望能多有一些摇滚。”―――不幸的是，这首歌引发了又一次全场大合唱，不少观众甚至应和着这首歌，伸出了右手的三个手指，做出了那个“摇滚与和平”的手势。<br />
