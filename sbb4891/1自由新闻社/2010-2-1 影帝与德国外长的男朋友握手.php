<br />
和谐社会的根本是每个阶层和社会群体的利益都得到保护，忽视同性恋者权益，对同性恋者视而不见，与和谐社会的目标背道而驰。我希望中国在这方面跟世界文明接轨，在今年春天的人大会议上设立保护同性恋权益的法律或法规。<br />
<br />
