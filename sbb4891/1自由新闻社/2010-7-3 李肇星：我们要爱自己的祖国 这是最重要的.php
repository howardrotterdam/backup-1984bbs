													<h2>李肇星：我们要爱自己的祖国 这是最重要的</h2>
						
						
																											<div id="postmessage_719496" class="t_msgfont">要干好一件事，必须多学习，既要好好学习自己祖国的先进文化，也要学习其他国家优秀的民俗或者文明，先从最简单的东西学习，打好自己工作的基本功。这是全国人大常委、外事委主任，前外交部长李肇星近日在“中外名家讲习堂”演讲时，面对500多位大中型企业高管们所发出的肺腑之言。李肇星的这次演讲题目是“胸怀国家发展纵横外交人生”，他从自己43年的外交生涯的点滴小事中，总结出了一些关于国家民族、人生哲学等诸多宝贵经验。<br />
<br />
　　尽管李肇星操着浓浓的山东口音，尽管听众都是大中型企业负责人，但演讲现场座无虚席，整个演讲过程中没有半点杂音，除了不想漏掉或错过演讲的内容之外，大家都对年过古稀的李肇星一贯的平民作风、谦逊好学、幽默风趣和注重细节等诸多品质，抱有深深的崇拜与敬意。<br />
<br />
在外交部工作了共43年的李肇星，到第42年的时候，有一天上班，他突然问自己――现在全世界有多少个国家？其实他回答不了这个问题，但也不想承认，或者不愿意让部下知道。于是就问部下，部下还以为是领导在考察干部呢。当时的一位姓罗的司长说：“报告部长，全世界有200多个国家和地区”。李肇星说：“小罗，看起来你是不知道，我没问你地区，我问的是国家。”随后，李部长又问一位姓刘的司长，“小刘”说：“联合国一共有162个成员国。”李肇星说，你怎么回答这个？我没有问你联合国有多少成员？我只问你全世界有多少个国家？其实，对于不知道的问题，正确的回答就是“不知道”，孔子说过“不知为不知”嘛，不要会什么就说什么。<br />
<br />
　　第二天，李肇星带着罗司长、刘司长三人一起，查资料、学习，最终一致意见是，全世界共有196个国家，但联合国只承认有162个成员国。实际上，国际上至今对什么叫“国家”这个简单的问题也没一个一致的解答。李肇星说，越是简单的问题越不容易回答。联合国中的巴勒斯坦，中国坚决支持巴勒斯坦的合法权利，并和巴勒斯坦建立了外交关系。但以色列等一些国家不肯承认巴勒斯坦。还有太平洋靠近赤道的地方有两个小国，他们不参加联合国，但是他们对我们很好，他们坚定地支持一个中国政策，承认台湾是中国的领土，这么好的国家能不建交吗？所以，我们和他们保持了非常友好的外交关系。<br />
<br />
1997年7月1日香港回归祖国，大的决策当时是邓小平决定的，李肇星参加过那次会见，他说，他一辈子都不会忘记那次会见。当时英国首相撒切尔夫人和邓小平会见，邓小平说，你(撒切尔夫人)这次来是谈香港回归问题的，香港要不要回归不要谈，我们肯定是要收回香港的。如果我们中国共产党人、我们新中国的人民政府不能在1997年7月1日之前收回香港，那么中国人民不会要我们！李肇星说，当时他很受震撼！其他的具体事怎么交接，怎么个仪式，他们怎么走，我们怎么进，那是下边的人去谈，这就是我们国家领导人的气魄！<br />
<br />
已至古稀之年，如何还能保持如此旺盛的精力？面对记者的提问，李肇星表示，他始终保持良好精神状态的秘诀，是自己一直觉得比较愉快，比较高兴。若说有不高兴的，就是觉得自己太不知足了，如果一天不看书就难受。<br />
<br />
　　作为一个非常成功的外交家，他对年轻人有什么建议？李肇星说，他在参观上海世博会的时候，遇到上海的一批志愿者。这些志愿者在家里连饭都不会做，现在不仅自己做饭还当志愿者。他们很感慨地说，这次锻炼对他们一辈子都有用。千里之行，始于足下。我们年轻人就要像这些志愿者一样，从一点一滴做起，做好自己的各项本职工作。对于国家而言，遇到别的国家挑衅的时候你不要怕，把自己管好比什么都强。目前，世界约有3000多种语言，3000多个民族，若干个文化。大家应该学会和睦相处，和谐交往。我们反对霸权主义，主张互相学习。我们要爱自己的祖国，这是最重要的。<br />
<br />
　　李肇星说：“我在美国那么多年，有一个最深的体会：你在美国如果不热爱自己的国家，你是交不到朋友的。美国人想这个人连自己的祖国都不爱，怎么能和他做朋友呢。所以热爱祖国是第一位的。比如涉及主权领土完整的问题，那肯定是寸土不让。”<br />
