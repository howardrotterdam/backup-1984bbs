<br />
　　海地总统普雷瓦尔当天说,已埋葬大约7000名地震遇难者。红十字会与红新月会国际联合会美洲地区负责人卡斯特利亚诺斯表示,海地政府预计四到五万人遇难。他还说,“至少300万民众受到地震的直接影响”。<br />
<br />
　　太子港总医院的拉罗什医生说,医院的太平间里早已放不下任何尸体,太平间外也已堆积了1500具。在人力不足的情况下,甚至出动垃圾车协助载运遗体,也让人类最基本的尊严变得荡然无存。<br />
<br />
　　一联合国人员奇迹生还<br />
<br />
　　联合国驻海地稳定特派团(联海团)14日宣布,海地地震已导致至少36名联合国工作人员死亡、64人受伤,另有188人失踪。遇难者中包括4名维和警察、19名维和士兵和13名文职维和人员。<br />
<br />
　　联合国秘书长潘基文15日上午在纽约联合国总部说，联合国将于当天下午就海地地震向国际社会发出5.5亿美元紧急募捐呼吁。<br />
<br />
　　潘基文14日说,一名爱沙尼亚籍联合国护卫人员在海地地震中奇迹生还,这名38岁的贴身警卫名叫塔尔莫・约维尔。救援人员先是听见了坍塌建筑物废墟中传来的刮擦物体的声音,随后确定了他的方位,并通过橡皮软管给他喂水。最终,救援人员于14日晨将他从废墟中救出。<br />
<br />
