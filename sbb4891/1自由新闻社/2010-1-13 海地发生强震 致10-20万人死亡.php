<br />
　　地震发生后,太子港仅剩一间医院在运作,医院满地都躺着伤员,至于医生则几乎全部葬身瓦砾堆中。医院经理盖那若施表示,“我应该有150个医生的,现在只剩不到20个”。由于伤员人数实在太多,医院最后只好开出“伤员巴士”,把伤员运到邻国多米尼加找医院治疗。<br />
<br />
　　自2004年开始在南非流亡的海地前总统阿里斯蒂德夫妇15日举行新闻发布会,表示将返回海地参与救援。阿里斯蒂德曾两度出任总统并两度流亡,目前住在南非。<br />
<br />
　　救援缓慢灾民堆尸抗议<br />
<br />
　　有目击者表示,目前太子港“满街都是人,漫无目的不断的走,眼神呆滞,空洞茫然,对尸横遍野的景象仿佛视而不见,他们脸上无助、绝望的神情,看了真的很难过”。<br />
<br />
