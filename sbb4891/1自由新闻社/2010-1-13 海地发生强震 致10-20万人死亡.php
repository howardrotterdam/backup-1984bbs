																											<div id="postmessage_432514" class="t_msgfont">海地6成建筑系豆腐渣 政府无自救能力<br />
2010-01-15 02:30:00　来源: 华龙网-重庆晚报(重庆)　<br />
核心提示：海地总统普雷瓦尔接受采访时说，总统府“国家宫”和他的私人寓所在地震中被夷为平地，他目前也无家可归。地震发生后，他和部下不得不踩着脚下死者的尸体逃生。<br />
<br />
重庆晚报1月15日报道 海地地震是两个世纪以来最严重的一次。英国达勒姆大学地球科学学系的瑟尔斯教授分析说，地震所释放的能量相当于50万吨黄色炸药爆炸当量。海地总理贝勒里夫称，死亡人数恐怕超过10万人。<br />
<br />
总统向全球求助<br />
<br />
海地总统普雷瓦尔在13日接受西方媒体采访时表示这次地震给太子港造成的损失简直“令人无法相信”，呼吁国际社会立即伸出援手。他说：“议会大厦塌了，税务大楼倒了，很多学校和医院轰然瓦解，街头满是尸体……请你们快帮帮我们！”<br />
<br />
幸存的普雷瓦尔说，总统府“国家宫”和他的私人寓所在地震中被夷为平地，他目前也无家可归。地震发生后，他和部下不得不踩着脚下死者的尸体逃生。他说，国会大楼和税务大楼倒塌了，而就在废墟下还能不断听到被埋在瓦砾中的人们呼救的声音。<br />
<br />
唯一医院人满为患<br />
<br />
据悉，目前一家由阿根廷人开设的医院是太子港唯一仍在对外运营的医疗机构。这家医院的负责人表示，很多医院在地震中已经倒塌，而这家医院目前是在超负荷运转。“大街上有很多等待救治的人，目前的形势很紧急，我们无法处理这么多伤员。”<br />
<br />
无法进入医院的伤者就被送到建立在停车场等空旷地带的临时医院。<br />
<br />
联合国维和司令遇难<br />
<br />
普雷瓦尔总统称，联合国驻海地维和部队司令也已经遇难。但是联合国并未确认这条消息。报道称，另外有16名联合国职员遇难，56人受伤,还有100到150名没有联系到。<br />
<br />
台湾“大使”骨折<br />
<br />
海地是台湾的“邦交国”，台湾“外交部”已同当地“使馆”人员全部取得联系，台驻海地“使馆”因地震受损，“大使”徐勉生骨折，其余人员无恙。<br />
<br />
可能暴发大瘟疫<br />
<br />
普雷瓦尔表示，自己非常担心灾区会发生大规模瘟疫。虽然目前对灾民来说最常见的疾病是因为吸入大量烟尘导致呼吸障碍和饮用污水造成的腹泻，但灾民患上登革热、疟疾和荨麻疹的风险正在不断增加。<br />
<br />
灾情・救援<br />
<br />
太子港最大的一座监狱倒塌，囚犯逃跑。<br />
<br />
近3000名警察在太子港负责治安，但人手吃力。<br />
<br />
救灾艰难，数万人仍被困于废墟之中。<br />
<br />
联合国总部降半旗向海地遇难者致哀。<br />
<br />
美国已派出“卡尔・文森号”航母前往海地救灾。<br />
<br />
美国记者徒手挖妻<br />
<br />
美国在海地的记者托普的妻子吉利安是一家国际救援机构在海地的工作人员，地震造成她所在的办公大楼整体倒塌。托普立即驱车160公里赶回首都，发疯般地在废墟中寻找妻子的下落。托普最终在办公大楼墙脚的瓦砾堆找到了已被埋10个小时的吉利安，用双手将还活着的妻子拉了出来。<br />
<br />
男子陷废墟发短信求救<br />
<br />
加拿大外长劳伦斯・坎农13日说，一名在海地的加拿大人身陷废墟后发短信向加拿大政府求助。他介绍，这名男子眼下被困在一处建筑物废墟中。“我们告知了加拿大驻海地使馆，要求他们如果可能的话，尽快展开救援。”海地境内大约有6000名加拿大人。<br />
<br />
分析<br />
<br />
为何如此惨重<br />
<br />
7级的地震其实算不上最严重的地震，但却为何导致海地如此多的人员伤亡？<br />
<br />
原因一：“豆腐渣”建筑<br />
<br />
海地60%的房屋都属于危险建筑，连遮风挡雨的功能都未必能实现，当然不可能防震和防飓风。<br />
<br />
<br />
原因二：过度砍伐森林<br />
<br />
为获取木炭来生火取暖做饭，海地人大规模砍伐城市周围的森林，土地已经逐渐变成了一个“不毛之地”。<br />
<br />
原因三：政府无自救能力<br />
<br />
海地长期内乱不断、经济不振，政府几乎没有自救能力，只能苦等外国救援。海地政府在地震发生后几乎无可作为。<br />
<br />
原因四：首都人口密集<br />
<br />
海地四分之一的人口都集中在首都太子港，所以伤亡巨大。<br />
