<br />
　　海地当地时间12日下午发生地震，此次地震震级为7.0级，震中位于海地首都太子港西部约15公里处，震源深度约为8公里。这是当地录得发生的最强地震。在1984年，该地区曾发生6.7级地震。<br />
<br />
　　美国地震勘探局的科学家估计，此次地震造成的财产损失和人员伤亡的数量将相当惨重。<br />
<br />
