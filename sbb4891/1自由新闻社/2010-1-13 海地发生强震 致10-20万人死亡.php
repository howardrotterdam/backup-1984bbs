																											<div id="postmessage_432530" class="t_msgfont">美国派航空母舰救灾<br />
<br />
　　美国南方司令部13日宣布，美国已派出“卡尔・文森”号航空母舰前往海地支援救灾，预计将于14日抵达海地。美国南方司令部司令道格拉斯・弗雷泽当天在五角大楼举行的新闻发布会上透露，“卡尔・文森”号接到前往海地的命令时正在执行训练任务，目前它已出发，预计将于14日下午抵达海地，主要提供人道主义物资运送和为直升机服务。<br />
<br />
　　弗雷泽还表示，美国正在认真考虑，派遣载有两千名美国海军陆战队的美国海军一艘大型两栖登陆舰前往海地参与救灾，如果有必要，还可以参与维持当地治安。美国国防部其他一些船只和海岸警卫队的船只，包括小型船舶、驱逐舰等也将被派往海地。除航母、舰艇和直升机之外，美国海军也派出一架P-3猎户座巡逻机，协助海地从空中侦察灾情。<br />
<br />
　　根据美国国务院公布的数字，大约4.5万美国公民在海地居住或工作，美国驻海地使馆总共有172名工作人员。为协调救灾，国务卿希拉里・克林顿13日已决定取消访问大洋洲三国的行程，立刻返回华盛顿。<br />
<br />
　　海地地震缘起板块碰撞<br />
<br />
　　地质专家13日说,海地地处两大地壳板块交界处的断层地带,属强烈地震多发地区。早在2008年,就有科学家曾预告过该国可能会发生大地震。一些地震专家认为,这一地区今后数年可能再次发生强震。<br />
<br />
　　按照法国地震学家亚恩・克林热的说法,海地12日发生的强震由于震源深度“非常浅”,“注定造成大范围损失”。另外,在2008年3月举行的第 18届加勒比地质会议上,5名科学家提交了一份报告,称“恩里基约―芭蕉园”断裂带可能会给海地附近地区带来“强烈地震灾害”。经证实,12日的强震就是发生在报告中提到的这条断裂带附近。有科学家推断,同一地区今后数年内存在“相当可能”再次发生强烈地震。</div>

							
							
							
							
