<br />
2010年01月15日07:48<br />
<br />
<img src="http://img.hexun.com/2010-01-15/122368578.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
大量灾区儿童面临严重身心创伤<br />
<br />
　　中新网1月15日电 海地7级大地震使首都太子港等地化为一片废墟，据估计，受灾人数达300万，死亡人数可能超过10万，当地参议员更估计有50万人罹难。现在，地震已发生两天，救援工作正加紧进行。包括中国在内的多个国家纷纷派出救援队伍，希望在72小时的黄金救援期内抢救出更多被困者。在救灾进行的同时，另一个问题开始引起人们的关注：灾民健康。<br />
<br />
　　太子港仅剩一家医院<br />
<br />
　　据香港《文汇报》报道，海地数百万灾民缺水缺粮缺电，废墟中尸横遍地。已有多个国际救援组织警告当地物资及人手严重不足，医疗灾难恐“万祸齐至”。海地总统表示，多数医院震毁了，没有足够容纳空间，还有很多伤者躺在街上。<br />
<br />
　　据香港大公网报道，位于太子港的多家医院在地震中倒塌，仅有一家阿根廷人运营的医院勉强正常运作。院方表示，医院已经超负荷运转，难以应付如此多的伤员。目前有800多名伤员在该医院接受治疗。<br />
<br />
　　专家担心瘟疫爆发<br />
<br />
　　报道称，地震令原已非常脆弱的海地医疗系统陷入瘫痪，专家担心各种瘟疫同时爆发。<br />
<br />
　　人道医疗组织无国界医生表示，他们在当地设立的临时诊治点有大批伤者涌入，许多人伤势严重。此外，当地的太平间也已爆满，有人担心被迫弃置街头的尸体会导致传染病爆发。<br />
<br />
　　数以万计的生还者在户外过夜，由于食物短缺、食水不洁，能否熬到救援物资到位，尚是未知数。太子港市中心的战神广场彷佛变成了一个巨型露天难民营，灾民只能从公共水池中舀不干净的池水解渴，帐篷布满灰尘，到处散发着尿臭，炎热的天气使味道更为难闻。许多人全身不是污渍便是伤痕，还有人躺在地上惨呼。<br />
<br />
　　英国广播公司记者走访了一家医院，他看见约一百具遗体躺在地上，灾民们就睡在尸体旁边。<br />
<br />
　　灾后儿童面临精神创伤<br />
<br />
　　此外，慈善组织救助儿童会警告，地震可能对200万名海地儿童造成终身精神创伤。他们表示，有些儿童在死尸堆中过夜，而且灾后大批孤儿涌现，他们可能要被迫独自面对肉体及精神伤害。<br />
<br />
　　无国界医生在当地有830名医护人员，当中大部分仍未能联络上。纵使在太子港还有可用的医院，但由于物资人手短缺，也难以处理近千名伤者。该组织发言人表示，灾民医疗需求非常急切：“每到一处，都有灾民包围我们，要求救治他们家人或其它重伤者。”<br />
<br />
　　救灾物资杯水车薪<br />
<br />
　　大规模的国际援助已在灾后展开，第一批数千吨包括食品、饮用水、医疗用品和帐篷等救援物资已经运抵海地，但这些物资对于流落街头的数百万人来说还是杯水车薪。仅有的救援物资引发了灾民争抢，产生了一些小规模的冲突。不少人只得依靠废墟中的食品艰难度日。<br />
<br />
　　在太子港附近的佩蒂翁维尔，灾民徒手挖掘一个被地震摧毁的超市，希望从那里获得生活必需品。法新社称，一些灾情严重的地方在地震发生时多个小时后都没有救护车、消防车现身，甚至连清理废墟的人也没有。还有报道称，太子港已经发生多起劫掠事件，一些灾民“拿走一切可拿的东西”。<br />
