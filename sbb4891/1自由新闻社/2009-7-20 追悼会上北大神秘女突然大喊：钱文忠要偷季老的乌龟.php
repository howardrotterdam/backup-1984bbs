																											<div id="postmessage_177535" class="t_msgfont">李玉洁曾想嫁季老？<br />
<br />
<a href="http://www.yangtse.com/wypd/whcz/200907/t20090725_673086_1.htm" target="_blank">http://www.yangtse.com/wypd/whcz/200907/t20090725_673086_1.htm</a><br />
<br />
2009年07月25日&nbsp;&nbsp;扬子晚报<br />
<br />
　　季羡林遗产案<br />
<br />
　　人物关系表<br />
<br />
　　“偷龟”点燃导火索，被卷入“遗产门”事件后，钱文忠连日在博客上发文，先是揭“白衣女人王如的真面目”，接着以“原形毕露的李玉洁”为题，把矛头转向与他认识了20年的李玉洁。李玉洁昨日给本报记者发来短信：“他们不仅合伙逼死了季老，现在又到处造谣气死我，因为我是唯一知情的人。”<br />
<br />
　　钱文忠不是季老学生？<br />
<br />
　　近几天，钱文忠不断地在其博客中发文，对被诬陷“偷龟”而大揭“白衣女人”王如和季老前秘书李玉洁真面目。为的是“能够让恩师在天之灵安息”。<br />
<br />
　　昨天中午，本报记者打通了正在北京301医院养病的李玉洁女士的手机，在稍等片刻后，她接了电话。从十分模糊的声音中，记者感觉到这位中风的老太太说话确实有些障碍。她简短地回答说，“盗画”事件是“没有任何根据的污蔑”，关于季老的遗产问题，她表示总会弄清楚的，但暂时不便说，否则会打草惊蛇。”然后挂断了手机。随后记者给她发了短信，过了20分钟，她回了短信，全文如下：“感谢你对我的信任，和对工作的负责！北大都知道，钱文忠根本就不是季老的学生，季老也从没有承认过。你可问北大原亚非研究所办公室主任杨守学，季老的真正弟子，也是季老唯一授权写季老传记的王树英。”<br />
<br />
　　记者又给钱文忠发去短信，他随即打来电话。听完记者的转述，钱文忠对“不是季老的学生”一笑而过，并嘲讽李玉洁“有点老糊涂了”，“满嘴跑火车”。“我是不是季老弟子，这些可能编造吗？这太荒唐了，有点胡说八道了。”<br />
<br />
　　李玉洁满嘴跑火车？<br />
<br />
　　钱文忠在22日博客中揭秘说，他认识李玉洁起码有20年以上。“起初没有打算写，因为她毕竟已是风烛残年，而且中过风。现在决定写，是因为她居然还敢粉墨登场。”钱文忠称，“我从大学一年级开始，就经常去季老家里，在他家中也见到过李玉洁几次，但她在季老家里极其不受欢迎，主要是因为李玉洁满嘴跑火车。”钱文忠回忆，“当年，季老的秘书李铮、女儿和女婿相继去世，儿子季承和季老闹了点别扭，两人不来往。就在这个时候，李玉洁不请自来，开始了对恩师10年的‘照顾’。李玉洁照顾季老不久，想方设法阻隔恩师和亲属朋友的来往；在给季老的版税方面她也动手脚。李玉洁曾经张罗过《牛棚杂忆》的出版，稿酬方面出现了严重问题，数字以10万计。后来李玉洁把钱送了回来。季老指示不必再追究了。”<br />
<br />
　　李玉洁：到处造谣气死我<br />
<br />
　　对钱文忠的“揭露”，记者又给李玉洁女士发去短信，问她对被指“盗画”事件幕后人如何看。很快，李玉洁回复说：“北大人都知道：季承、钱文忠十句话有十一句都是假的，他们不仅合伙逼死了季老，现在又到处造谣气死我，因为我是唯一知情的人……”李玉洁向记者强调，季老生前，确实多次向她提及，要把遗产捐给北大。在她当秘书期间，季老也给过她钱，但她都没要，而是拿着季老的身份证，将钱存到季老的卡里了。她还透露，季羡林生前立有遗嘱，现在就存放在北大的图书馆，明确表明不留给儿女一分钱。她表示，现在还不是公开遗嘱的时候，“因为季承已被钱文忠恶毒控制，而季承又不仁不义，把钱看得比季老还重，故暂不方便公布。” <br />
<br />
　　李玉洁隐匿季老20笔存款<br />
<br />
　　钱文忠在博客中写道，恩师去世前不久，季承查出，季老在中国银行有20多笔存款，存单被李玉洁隐匿，数量惊人，其中有一笔就达25万美元，后来知道是范曾先生捐给恩师建资料馆用的。范曾先生有亲笔信，信中要李玉洁不要动这笔钱。其次，在李玉洁掌握钥匙、与恩师相关人员根本无法进入的一处房子里，发现了包括苏东坡《御书颂》在内，共30多幅恩师的藏画精品。在场有北大多位工作人员，有签署的清点文件为据。<br />
<br />
　　李玉洁一心嫁季老？<br />
<br />
　　李玉洁的先生是北大东语系教授杨通方，“文革”前曾住在季老对门，1978年社科院南亚所成立，季羡林任所长，李玉洁是工作人员。1996年季老的得力秘书李铮退休，1998年突然去世，李玉洁逐渐承担起主要工作，直至2006年8月，李玉洁突发脑溢血住院，杨锐成为主要秘书。李玉洁今年81岁，她曾对媒体表示“跟随季老五十多年”。季承说：“这并非事实，从1995年至2006年，她在季老身边大概11年的时间。”谈起李玉洁，季承说，“1995年以后，她撇开丈夫，专门‘攻’季老，最终的目的是要嫁给他，得到他的财产。”季承认为李玉洁接近季老是有目的的。“我与父亲重逢后，曾经跟父亲开玩笑：您为什么不娶李玉洁？他笑了说，她这个人不可靠。季老心知肚明，也不装糊涂，绝对不会让她得逞。”本报记者 蔡 震</div>

							
							
							
							
