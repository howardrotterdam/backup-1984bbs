																											<div id="postmessage_169618" class="t_msgfont"><p align="center"><strong>季羡林遗产风波出现神秘女子</strong></p><br />
<br />
<p align="center">追悼会闹出乌龟被偷疑云，一白衣女子声称季羡林学生钱文忠指挥季承争产</p><br />
<br />
<p align="center">发布时间：2009-07-20&nbsp;&nbsp;版次：AA12 版名：国内 稿源：南方都市报 </p><br />
<br />
<p align="center"><a href="http://www.nanfangdaily.com.cn/nfjx/200907200002.asp" target="_blank">http://www.nanfangdaily.com.cn/nfjx/200907200002.asp</a> </p><br />
<br />
<p align="center"><u>摘要：昨日上午11时许，追悼会接近尾声。突然，一位自称季老学生、北大教授的白衣女子大喊：“钱文忠要偷季老的乌龟！”</u> </p><br />
<br />
<p align="center"><img src="http://pics.nddaily.com/b/15/b1563a78ec593375/Thumb/13661/dcga122005.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /></p><br />
<p align="center">出现在季羡林遗体告别仪式上的白衣女子自称是北大教授。</p><br />
<br />
&nbsp; &nbsp; 本报讯 昨日上午11时许，追悼会接近尾声。突然，一位自称季老学生、北大教授的白衣女子大喊：“钱文忠要偷季老的乌龟！”<br />
<br />
&nbsp; &nbsp; 神秘女子现身追悼会<br />
<br />
&nbsp; &nbsp; 此时，一名身着深色衬衣的男子在众人的斥责中匆匆从人群中挤出，消失在人群里。记者看到，此人并非钱文忠。<br />
<br />
&nbsp; &nbsp; 白衣女子说，因为季老生前最爱养两种动物，一种是乌龟，一种是猫。昨日，季老的管家方咸如特别把家里的乌龟带来，与季老做最后的告别。没想到钱文忠想偷走这个乌龟，他将装乌龟的鱼缸抱出礼堂，后交给他的一位朋友。偷窃行为立即被方咸如发现并予以制止。<br />
<br />
&nbsp; &nbsp; 该女子把所有矛头直指季羡林关门弟子钱文忠。她气愤地表示：“从字画被盗风波到季老的死都是一场阴谋！是钱文忠在背后指挥季承抢夺财产！”<br />
<br />
&nbsp; &nbsp; “季老不能白死，所有迫使季老死的人都要付出沉重的代价！”白衣女士说，季老于2003年、2006年和今年6月13日三次说过，要把全部遗产全部捐给国家，不给子女留一分钱。因此北大也不会给他们一分钱。<br />
<br />
&nbsp; &nbsp; 季老北大房屋被人动过？<br />
<br />
&nbsp; &nbsp; 接着，该白衣女子把季羡林干女儿施汉云和季羡林管家方咸如推到媒体面前。<br />
<br />
&nbsp; &nbsp; 施汉云称，在2001年季老90大寿的时候，他们专门从香港用金子打造了“福禄寿”送给季老，但现在不知去向。她说她有照片为证：“季老死后，我还去他家拍了一些照片。但记者要求当场观看照片时，她又说：“你们等着吧，会有书出来。”方咸如也表示，他近期进入朗润园13号公寓时，原先摆在客厅的福禄寿已不知去向。<br />
<br />
&nbsp; &nbsp; 施汉云用“人去楼空”一词形容现在季老家中的情况。“很多我以前整理的东西位置都发生了变化。季老生前房间干净整洁，现在乱七八糟。”<br />
<br />
&nbsp; &nbsp; 白衣女子说，北大保持沉默，是因为家丑不可外扬，为季羡林的声誉而考虑。但她并未说，她代表的是北大立场。<br />
<br />
&nbsp; &nbsp; 昨日，记者多次拨打北大发言人赵为民手机，对方一直处于无人接听状态。<br />
<br />
&nbsp; &nbsp; 回应<br />
<br />
&nbsp; &nbsp; “偷龟一说纯属谣言”<br />
<br />
&nbsp; &nbsp; 季承称家中物品已保管好，希望有些人不要找接口制造混乱<br />
<br />
&nbsp; &nbsp; 昨日下午4时许，季承告诉南方都市报记者，他也不知道该白衣女子是谁，但可以肯定这种话完全是谣言中伤。<br />
<br />
&nbsp; &nbsp; 钱文忠：偷龟事件全不知情<br />
<br />
&nbsp; &nbsp; 季承说，据他所知，季老最近是在养两只乌龟。他在现场没有看到乌龟，也不知道乌龟摆在了季老的遗体旁。至于说钱文忠“偷乌龟”，他表示，纯粹是胡扯。<br />
<br />
&nbsp; &nbsp; 钱文忠则向本报记者表示，他直接从301医院前往八宝山的，在遗体告别仪式现场的季羡林亲属房间见到了这两只乌龟，但是谁带来的、为什么带来以及此后发生的“偷乌龟”事件他完全并不知情。<br />
<br />
&nbsp; &nbsp; 对于季老家已经乱七八糟，季承回应，家中物品他已经妥善保管了。他们家的东西，别人现在没有权力，也没有义务去管理这些事情，也希望有些人不要借口这些事情，来制造混乱，否则要负相应责任。<br />
<br />
&nbsp; &nbsp; “现在有一个很奇怪的现象，就是我们季家的东西别人在管。而且那个看门的(方咸如)我们已经把他解雇了，他还在那里说东道西。”他说。<br />
<br />
&nbsp; &nbsp; 季承认为，小方这样做是有人在后面给他出主意。<br />
<br />
&nbsp; &nbsp; 文化遗产到底如何分配？<br />
<br />
&nbsp; &nbsp; 季老遗产到底如何处置？季承昨日对南都记者说，他们已开家庭会议，一致的意见是，季老的文化遗产属于社会，我们会按照人情、法律，来妥当处理这些问题。目前没有具体的方案。<br />
<br />
&nbsp; &nbsp; 他对记者说，“外边任何对于遗产的谣传，都不要轻信。网上很多人说，季承想吞下这部分巨额遗产，就是为巨额遗产而来的。像这些说法都是不实的。”<br />
<br />
&nbsp; &nbsp; 钱文忠则表示，7月17日，季承已授权他在他的博客上对季老的遗产如何处置进行发布。即“季羡林先生的文化遗产终究属于全社会和全民族”。<br />
<br />
&nbsp; &nbsp; 季承说，去年12月6日，季羡林写了一份委托书，在场的人还以证人的身份在上面签名。委托书内容为“全权委托我儿子季承处理有关我的一切事务。暨。季羡林。戊子冬。2008年12月6日于301医院”。<br />
<br />
&nbsp; &nbsp; 季承还说，目前北大方面的态度是，尊重季老的遗愿。他认为，“全权委托季承处理一切事务”就是季老的遗愿之一。<br />
<br />
&nbsp; &nbsp; 疑问<br />
<br />
&nbsp; &nbsp; 神秘白衣女子是谁？<br />
<br />
&nbsp; &nbsp; 1 她是北大教授？<br />
<br />
&nbsp; &nbsp; 该白衣女子到底到谁？她只是说，自己是季羡林的学生，现在在北大当教授。但记者问她名字时，她始终不愿透露。<br />
<br />
&nbsp; &nbsp; 方咸如称，这位白衣女子是北京大学公共管理学院一位老师，姓王。<br />
<br />
&nbsp; &nbsp; 昨日下午，施汉云接受本报记者采访时称，该白衣女子是一位记者，但随即又改口说，她是北大的老师。<br />
<br />
&nbsp; &nbsp; 季承说，他不认识这位女子。钱文忠则说，季羡林的女学生没有在公共管理学院的，全部在外文学院。<br />
<br />
&nbsp; &nbsp; 记者发现，该白衣女子的前后说法也不一致。最开始，她指着身着深色衬衣的男子说，“钱文忠要偷乌龟。”但记者反驳她，该男子不是钱文忠时，她又改口了，说“该男子是钱文忠派来的。”<br />
<br />
&nbsp; &nbsp; 2 季羡林有干女儿？<br />
<br />
&nbsp; &nbsp; 该白衣女子称，施汉云是季羡林干女儿，知道很多内幕。但施汉云自称，她其实是季羡林的小友，称季羡林为爷爷。<br />
<br />
&nbsp; &nbsp; 记者查证，施汉云其实是已故著名出版家、香港汉荣书局创办人石景宜的秘书，跟随石老20多年。而石景宜与季羡林在生前是好朋友。<br />
<br />
&nbsp; &nbsp; 季承和钱文忠昨日说，他们知道施汉云这个人，但与她都并不熟悉。<br />
<br />
&nbsp; &nbsp; 3 乌龟为何会带到遗体告别现场？<br />
<br />
&nbsp; &nbsp; 季老的遗体告别现场，为何会有乌龟到达现场？<br />
<br />
&nbsp; &nbsp; 方咸如说，是他把乌龟带来的，因为季老生前最爱养两种动物，一种是乌龟，一种是猫。所以他把季老生前在北大家里的乌龟带来，与季老做最后的告别。但是钱文忠对此表示质疑，方咸如只是季老家的一个护工，他有什么权力私自拿季老的东西？<br />
<br />
&nbsp; &nbsp; 4 病逝前不久讲过遗产如何分配？<br />
<br />
&nbsp; &nbsp; 该白衣女子称，季老于2003年、2006年和今年6月13日三次说过，要把全部遗产全部捐给国家，不给子女留一分钱。<br />
<br />
&nbsp; &nbsp; 但季承表示，自从去年11月7日与父亲和好以来，他每天都陪着父亲，可以肯定的是这大半年里，父亲没有立过任何遗嘱，也没有讲过遗产分配问题。<br />
<br />
&nbsp; &nbsp; 5 季羡林父子13年未见另有隐情？<br />
<br />
&nbsp; &nbsp; 施汉云昨日还表示，造成13年父子未见的主要原因在于季承个人的一些行为导致季老不愿见他。<br />
<br />
&nbsp; &nbsp; 施汉云说，这13年来她每个月都能见到季羡林，并给他送营养品。“我和季老是萍水相逢，但每次去看他都没碰到阻拦，如果真的想见季老难道见不到吗？”<br />
<br />
&nbsp; &nbsp; 她表示，造成13年父子未见的主要原因在于季承个人的一些行为导致季老不愿见他。“季老比较传统正直，他不理解季承与小保姆之间的关系。”<br />
<br />
&nbsp; &nbsp; 她还表示，之前季承母亲彭德华生病期间，季承不愿承担部分医疗费用，季老感到不高兴。<br />
<br />
&nbsp; &nbsp; 季承昨日回应，父子不和的原因有比较复杂的历史原因，不是简单的像施汉云讲的为了钱。至于他跟保姆晓华的关系，季先生是同意的。具体原因不是一句话能说清楚的，以后会向大家解释清楚。他认为，施汉云的说法是不负责任的，是中伤。<br />
