<br />
概述<br />
―C<br />
古鸽是一种目前在中国境内濒临灭绝的鸽属鸟类。此鸟起源于北美洲，据生物学家考证其祖先生活在相当于今天的美国加州圣克拉拉县的山景城附近。在20世纪末至21世纪初的一段时间，它曾经遍布世界各地，但在2010年3月23日以后，该鸟类开始大规模往中国南部沿海的一个港口迁徙，从此在中国大陆绝迹。这对于生物学家来说是一个难解的谜团。<br />
据环保人士的谨慎假设，怀疑该鸟类的异常行为和最近全球气候极端化，特别是中国近几年来频发的大面积生态、环境、气候和地质灾害有关。因为根据美洲印第安人的传说，这种鸟有一个相当重要的习性，印第安语称之为“don’t be evil”，翻译成中文就是“害怕河蟹”。遇到河蟹泛滥的环境，它没有像草泥马一样顽强地生存下来，而是举全族迁徙，这被全球各地的一些动物爱好者所鄙视，纷纷称其为生物界的耻辱。<br />
由于古鸽温顺的性格，快速的飞行能力和准确的导航能力，敏锐的眼光，强大的寻物能力，它长期以 来被当做常用的寻物禽、信鸽和导航禽，是一种人民群众喜闻乐见的鸟类，它为人类文明的发展做出了不可磨灭的贡献。<br />
古鸽被认为是目前所有鸽属鸟类的真正祖先。所以被称为“古鸽”。<br />
<br />
外形特征<br />
―――C<br />
身披蓝、黄、红、绿四色羽毛，比家鸽体型稍大。鸣叫声和英文单词 “googol”类似，美洲印第安人认为其叫声代表了”难以置信的数量“的意思，数学家经过严谨的考证计算，认为这个数量大概是10的100 次方。<br />
虽然古鸽羽毛有四种颜色，但它主要集中在头部。其身体通体透白，在天上飞翔时，往往和云层融为一体，显得非常低调。动物学家认为，古鸽的这种体貌特征是进化赋予的，它的彩色头部有利于在族群生活和求偶中获得个性身份和独特吸引力，而其低调的白色有利于它成为大自然的观察家角色，有效排除各种生物的骚扰，专心收集信息，同时减少消耗和进化成本。所以，古鸽简单的外貌折射了它不简单的能力。<br />
<br />
生活环境<br />
―――C<br />
古鸽具有超强的环境适应 能力，并且总能在短期内进化出本地化的新品种。例如目前数量较多的有美国古鸽、日本古鸽、英国古鸽等亚种。由于生物考古学的研究证明古鸽起源于美国，一般 我们把美国古鸽简称为古鸽，其它地区的亚种冠以当地国名。<br />
但是古鸽由于具有十分抗拒和害怕河蟹的习性，它没能在中国大陆境内成功进化出本地化亚种。但是这并不能全部解释古鸽为何在中国大陆已经繁殖出规模庞大的种群数量，却又最终发生大规模迁徙行为。部分动物爱好者表示质疑，如果说古鸽真的害怕河蟹，它当初为何又明知河蟹在中国的食物链属于顶端生物，却又偏要前来。唯一的解释是，在中国大陆日益增加的雅克蜥对古鸽来说是世界上其它地方难得的美味。<br />
生物学家正在努力研究是否这预示着中国大陆将发生严重的生态灾难。初步的研究表明，古鸽的离去很可能导致另一种长着熊爪，酷似古鸽，却又习性不同的猛禽类――鄱灸瘢这种古书中传说的本土鸟类数量呈爆炸性增长，最终迫使中国大陆居民不得不使用这种带有剧毒、性情凶狠、只以中文作为鸣叫声、以钱币为食的上古神兽级猛禽，以代替古鸽的部分功能。古词有云：“纵你驯它千鄱荆那人却在，瞪火烂衫怵”（不管你怎样想尽办法试图驯服鄱灸瘢它最终会让试图驯服它的人瞪大眼睛火冒三丈衣衫褴褛惊吓不已）<br />
<br />
生活习性<br />
―――C<br />
群居，一个国家的古鸽族群有各种不同的擅长功能。其中以美国古鸽功能最多。<br />
古鸽以各种印有文字的物体为食，并会自动评估该食物的权重，最终以极复杂的算法决定下一次进食 的优先选择顺序。<br />
已知古鸽的天敌有河蟹、中国蚊祚蟹等蟹类生物。<br />
传说：古鸽对原产于中国的雅克蜥具有强烈兴趣，有人以此解释古鸽来中国的原因。<br />
<br />
种群现状<br />
―――C<br />
全球约有 120,000,000,000只古鸽，但是中国大陆境内目前已经基本绝迹，原中国古鸽大规模往南部香港迁徙。所以，目前全球古鸽的种群数量呈下降趋势。<br />
