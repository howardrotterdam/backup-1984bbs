													<h2>关于英语退出高考的构想引起关注</h2>
						
						
																											<div id="postmessage_453344" class="t_msgfont">关于英语退出高考的构想引起关注<br />
2010-01-26<br />
中国教师奖励基金会秘书长杨春茂在一次会议上转述了关于“取消高考英语考试”的构想。这一构想引起广泛关注。有专家认为，在条件成熟的时候，对“取消高考英语考试”、将英语考试变成社会考试的建议可予以考虑。自由亚洲电台记者杨家岱的采访报道<br />
<br />
纵观各家媒体的相关报道，提出取消高考英语考试、将英语考试变成社会考试构想的人认为，取消高考英语考试，可以减轻学生的压力，从而使学生对于学习英语有更大的自信、兴趣和主观能动性。这样看来，提出取消高考英语考试构想的人并不是不重视英语，而是在谋求把英语学得更好的途径。当然，也有非常重视英语学习的人不赞成英语退出高考的。<br />
<br />
原中国科技大学副校长方励之认为，取消高考英语考试的具体构想以及一旦实施后如何运作，现在还不是十分清楚： <br />
<br />
“作为社会考试什么意思呢？是不是英语就是第二国语了?&nbsp;&nbsp;因为你考这个当然大家普遍都要考，而且有要求就相当于第二国语了。”<br />
<br />
但是，方教授表示，在条件具备的情况下，比如在中小学英语教学已经得到加强的情况下，取消高考英语考试是可以考虑的；但是无论采取什么考试办法，英语教学的标准不能降低：<br />
<br />
“也就是说中小学的这个外语要加强或者是英语作为一个必修的第一外国语倒也可以，但是没有一个国家统一的标准，我觉得可能质量会有变化。现在你真正普遍实行所有招工都考英语的话，也没这个能力吧，根本没有这个能力，弄不好所有社会普遍化以后，弄不好出来一种中式英文 ，现在已经出现中式英文了。” <br />
<br />
方教授说，对于英语没有学好、但其它学科很有天分的学生，要注意不要埋没了他们： <br />
<br />
“如果他数理化反应特别好，但是就是英文不行。这种情况还不少说实在的。我遇到好多就是这样，包括爱因斯坦，那个英文很差很差的。” <br />
<br />
记者：那您这个意思您不会是说这个高考取消这个英文考试？<br />
<br />
方教授：“不要取消，这应当有多种路子，你可以看一看，推荐了或者什么其他路子。”<br />
<br />
华人政治评论家孟玄表示，取消高考英语考试未尝不是一个好想法，但前提是要保证社会上考试机构的可信性：<br />
<br />
“我觉得也未尝不是一个好的想法，但是你必须第一要社会上的这个机构，就是测验的机构是一个可信性很高的。像美国来讲，它有所谓托福考试，那么这是给外国学生考他测验这个东西，那么美国学校是那它作为参考。而且很重要的参考。” <br />
<br />
孟玄说，中国社会的英语热在一定程度上反映了人们的开放心态。他说，一个受教育的人今后应当是双语的： <br />
<br />
“这个应该算是好事，就表示中国人愿意开放，因为不管怎么讲，英文现在还是全世界最通行的外国语言。法文比较差一点。那么越来越通行。你在那一个行业除非你是完全是中文的环境里头，否则的话呢，你要跟外面接触你就要会英文。中国人普遍自发地愿意去学英文，这是一个很好的倾向，就表示你愿意吸收外面更大的文化。英文可能变成其它全世界最共同的语文。一个人最少在未来这个世界是需要二种语文的。应该是双语的。”<br />
<br />
方励之表示，英语的使用在中国十分普遍： <br />
<br />
“实际上我看到（当然我没有回国）但我偶尔看国内电视，好像所有牌子下面都有英文，几乎连商店都是这样。当然英文很糟糕，不过到处都是英文。”<br />
<br />
这两天有关中国高考的另外一条消息是，上海市部分大学自主招生不考语文。这个作法被一些人指责为学科歧视。方励之教授也对忽视语文学科表示不以为然： <br />
<br />
“你这个基本的中文必须要清楚，这个写文章就是表达思想嘛，表达意思。如果你中文都表达不好的话，特别你母语是中文，如果你母语是中文，如果你表达不好的话，英文也有问题。不可能你学了英文，你英文自然就表达好了，这样不行的。这写文章不光是一个认单词和语法结构，这个要整个要有一篇一篇的思想。” <br />
<br />
评论家孟玄也说， 语文和数学是必须掌握的做学问和服务社会的基本功： <br />
<br />
“大学不是就已经非常专科性的东西，而这个汉语其实是我们的母语，处理汉语的能力跟这个处理数理化的能力是最基本的两个东西，就是训练你的思维能力。你作为一个大学生基本的教育，还是应该把这个最基础的东西来平衡地来发展。这个语文的能力实际上是你的智商的一个表现。一个是数学，一个是汉语。这两个掌握的能力是最能够看出这个小孩子本身的智力状态的。” <br />
<br />
以上是自由亚洲电台记者杨家岱的采访报道。</div>

							
							
							
							
