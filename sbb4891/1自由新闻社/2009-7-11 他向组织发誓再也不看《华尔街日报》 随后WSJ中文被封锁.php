																											<div id="postmessage_158114" class="t_msgfont">丁刚：为什么我不再看《华尔街日报》<br />
<a href="http://opinion.huanqiu.com/roll/2009-07/511615.html" target="_blank">http://opinion.huanqiu.com/roll/2009-07/511615.html</a><br />
<br />
从今天开始，我不再是《华尔街日报》的读者。我已经把这家报纸的网址从我的浏览器中的“书签”里删除，我还把每天通过电子邮件订阅的《华尔街日报》中文版划入垃圾邮件。<br />
<br />
　　我将利用一切机会，请求我的朋友和同事，不要再看《华尔街日报》，也不要再去浏览《华尔街日报》网站，更不要在它的网站上发帖子。我将对每一个中国同行说，请不要引用《华尔街日报》的报道或评论。我还要对那些订阅《华尔街日报》的中国人说，不要再浪费你们的美元了。<br />
<br />
　　10多年来，作为《华尔街日报》的忠实读者，我每天都要阅读这个在国际上颇有声望的报纸。在国外工作时，我还是它的持续订阅者，并从中获得了很多有用的信息。直到我在写这篇文章之前，我的信箱里每天都会收到《华尔街日报》中文版。<br />
<br />
　　坦率地说，《华尔街日报》近年来的对华报道、评论越来越令我失望，其中有不少都带有偏见与无知。考虑它所提供的财经新闻和评论还有些价值，我仍然没有放弃阅读这份报纸。但是，此次《华尔街日报》对乌鲁木齐“7・5”事件的报道，越来越让我无法容忍。它不再只是以偏见和无知来看中国，而是公然站在了恐怖分子一边，成了他们的代言人。<br />
<br />
　　连日来读了数篇《华尔街日报》亚洲版的报道，有的在文中提到维族用的是“抗议者”，提到汉族用的是“暴徒”；有的报道把暴乱称为“流血事件”，并说是由维族人抗议“不公正待遇”而引发的……我起初认为这是西方媒体的偏见所致。但8日网站上的变化，简直无法让人容忍。“世界维吾尔代表大会”首领热比娅的头像在显著位置出现，链接的是她的文章“维吾尔的真实故事”。而另一篇找不到署名的文章的标题是“乌鲁木齐的起义”，副题是“北京镇压了穆斯林少数民族”。用不着再更多地引用这些文章里的话，因为这些胡言乱语翻译过来，是对视线和心理的极大污染。<br />
<br />
　　是的，《华尔街日报》的编辑们可以辩解说，这就是平衡与公正。但不知他们想过没有，如果在“9・11”事件发生后，中国某家媒体用“纽约的复仇――穆斯林少数民族对美国霸权的抗争”这样的标题，来评论这件事情，他们还会认为这是平衡、公正吗？！<br />
<br />
　　请记住，那些残忍到连几岁的孩子都不放过的暴徒，在哪个法治国家，用哪条法律来解释，都是暴徒，都是恐怖分子！<br />
<br />
　　阅读这些报道和评论，我有一种被羞辱的感觉。我可以忍受偏见，但绝不可以忍受羞辱。我相信，世界上没有一个读者愿意去阅读一份让自己感到被羞辱的报纸。《华尔街日报》可以不在乎失去我这一个读者，但我绝对在乎自己的人格和民族的尊严。<br />
<br />
　　当然，我也可以选择写文章、发帖子的方式，反驳这些报道和评论，但我觉得和恐怖主义代言人辩论，根本就是一件有损自己名誉与形象的事情。最好的方法就是放弃阅读《华尔街日报》，眼不见心不烦，让自己的心灵更清静一些。<br />
<br />
　　从今天开始，我将对每一个不再阅读《华尔街日报》、不再给《华尔街日报》写稿的中国人说：“谢谢！”</div>

							
							
							
							
