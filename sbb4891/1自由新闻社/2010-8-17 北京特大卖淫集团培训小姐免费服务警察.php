													<h2>北京卖淫集团敛财3000万：警察为其通风报信</h2>
						
						
																											<div id="postmessage_786578" class="t_msgfont"><a href="http://news.qq.com/a/20100817/000212.htm" target="_blank">http://news.qq.com/a/20100817/000212.htm</a><br />
[导读]该集团猖獗6年未被查获，除自身谨小慎微外，更关键的是警察为其通风报信。作为交换，该民警可以去洗浴中心免费嫖娼。集团有整套严密的“操作规范”，收买上千的哥招揽客源。<br />
<br />
近日，一起特大卖淫集团案在北京市二中院审理，集团头目王爱国一家三口被控在6年间开办或承包5家洗浴场所，组织、领导众多卖淫人员从事卖淫3万次以上，获利3000万元以上，集团54名被告人被公诉。 <br />
<br />
案件材料显示，该集团之所以猖獗6年未被查获，除自身的谨小慎微外，更为关键的是有一名警察为他们“放哨”。每当警方检查前，这名民警都会为卖淫团伙通风报信。作为交换，该民警可以去洗浴中心免费嫖娼，并涉嫌收受集团老板王爱国给予的一辆价值7.15万元的轿车。上周，这名警察涉嫌受贿罪被公诉。<br />
<br />
1 结识查店警察<br />
<br />
一手经营起5家洗浴场所的老板王爱国是北京人，现年54岁，初中毕业后曾在北京汽车制造厂工作，1985年辞职后经商，开过歌厅和啤酒屋。 <br />
<br />
2003年，王爱国和妻子孙美玉开始经营洗浴中心，其中有他们全资的，也有与人合股的。 <br />
<br />
据孙美玉供述，他们经营的每家洗浴中心都设有卖淫场所。2003年9月，因其中一家被查出有卖淫嫖娼人员，店里两名领班还被朝阳法院以容留卖淫罪各判处有期徒刑两年。但为了招揽生意、多挣钱，他们一直允许卖淫行为存在，而如何防止被警察查获，成为一个棘手的问题。 <br />
<br />
2007年上半年，崇文公安分局民警崔大力(化名)和队里的同事去崇文区法华寺南里的北京阳春白雪洗浴中心检查工作时，认识了该洗浴中心老板王爱国。当时，王爱国还经营着东城区的金来宝浴池、朝阳区的海上大都会洗浴、崇文区的黄河京都大酒店洗浴部、朝阳区的海日裕阳国际休闲会所。 <br />
<br />
崔大力现年46岁，河北省香河县人，大专文化程度，案发前任北京市公安局崇文分局民警，主任科员，一级警督。据崔大力自述，他早年在部队服役，之后一直在崇文公安分局工作，先后在前门大街派出所、分局治安处任职。2007年8月，他开始在崇文公安分局治安支队行业场所管理队任内勤民警。<br />
<br />
2 全程消费免单<br />
<br />
认识崔大力后，王爱国开始请崔大力吃饭，并提议让自己的儿子王思嘉认崔大力做干爹。现年25岁的王思嘉曾到英国阿伯丁大学留学。认识崔大力时，他刚毕业回国一年，没有另外找工作，而是帮父母经营洗浴中心。 <br />
<br />
为了和崔大力处好关系，王爱国和王思嘉在崔大力去阳春白雪洗浴中心消费时，告诉经理全部免单。在和经理介绍崔大力时，王爱国直言：“这是分局的崔哥，以后崔哥来了好好照顾。” <br />
<br />
领会老板的意图后，这名女经理大胆问崔大力：“崔哥，要不要给你安排一个？”“行，安排一个吧”。就这样，原本负责查处卖淫嫖娼的民警，自己却开始嫖娼。 <br />
<br />
崔大力供述，此后，他每次去阳春白雪，经理都会主动给他安排小姐。最多的时候，他一周嫖娼三四次。收银员开出的支出凭单上注明“招待崔哥”，然后由小姐签字，店里会给小姐提成50元。 <br />
<br />
为了把崔哥照顾好，经理还特意向店里的“妈咪”、小姐透露崔大力的身份，结果一名小姐告诉经理说，崔哥嫖娼时已介绍自己是警察。 <br />
<br />
此外，崔大力还带朋友去嫖娼，他自己不给钱。有时候，他的朋友会给每个小姐300元小费。<br />
<br />
3 警察成线人<br />
<br />
实际上，崔大力在洗浴中心并不是真的受欢迎。阳春白雪的经理向警方供述，他们并不爱接待崔大力，“他一去，我们店里都围着他转，他又不给钱”。而他们之所以讨好崔，就是因为他的特殊身份。“他是警察，能在检查之前给我们一些消息。” <br />
<br />
该经理介绍，崇文公安分局经常去店里检查。有时，老板王爱国、王思嘉事前会给她打电话，告诉她“这两天有检查的，注意点”，这样，他们在接活时就要小心，如果老板让停活，他们就停。这名经理说：“王爱国私下说过，他自己有关系、有人。孙美玉开会时也说，我们要进去了就扛住别说，他们可以找人捞，要是承认了，就没法儿捞了。”崔大力能事先告诉他们消息，为他们从事卖淫活动壮了胆。 <br />
<br />
对此，崔大力在接受讯问时承认自己为洗浴中心通风报信。他说，一般市公安局统一行动或者各区公安分局互查时，他会提前知道，并给阳春白雪的经理打电话，“告诉她今天要检查，让她小心些别出事”。 <br />
<br />
对于自己给他们通风报信的原因，崔大力总结说：“从我个人角度而言，我怕市局检查时把小姐抓了，小姐把我供出来，对我不利；从王爱国的角度而言，我就是他的眼线，一有大规模检查，我就通知他们，不容易出事。”<br />
<br />
4 受贿捷达轿车<br />
<br />
除了给王爱国通风报信，崔大力还帮王爱国联系过一桩承包宾馆客房的生意，并涉嫌收受王爱国给予的一辆价值7.15万元的捷达轿车。 <br />
<br />
崔大力说，2008年10月，他的朋友张某和他说，宝鼎大厦有80间客房要对外承包，租金便宜，可以合作承租来赚钱。“我也想挣点钱，提高生活质量”，于是，他将这桩生意介绍给了有资金实力的王爱国，后者成功将客房承包。 <br />
<br />
当时，王爱国对张某说：“老崔给介绍的，他也挺苦的，给他5%的干股，但不用出钱，什么都不用他负责。”崔大力对此默认，但出于谨慎，他没有在股东协议上签字。 <br />
<br />
王爱国供述，2008年底，崔大力几次向他提出自己没有车，到哪里办事都不方便。“当时我就明白了，崔大力是让我给他买辆车。” <br />
<br />
王爱国说，考虑到自己的买卖在崔大力的地面上，得罪不起他，又需要他罩着，同时他又给自己联系了生意，于是就问他想买什么车。当听到后者就想买辆老款捷达时，他觉得要求不高，爽快答应了。去年3月26日，王爱国和崔大力一起去买了辆7.15万元的捷达车。 <br />
<br />
对于王爱国送车一事，崔大力不否认，但他称那是王爱国看他没车，主动以宾馆提前分红的名义给他买的。 <br />
<br />
不过，崔大力最后还是承认自己涉嫌受贿，“王爱国给我干股、送车，让我去洗浴中心消费免单，都是利用我这个人民警察手中的职权，给他站岗、通风报信，使他逃避法律的制裁”。<br />
<br />
5 顾客被敲诈报警<br />
<br />
尽管有崔大力为之通风报信，但王爱国的洗浴中心最终还是难逃法网。直接导致警方出手的，是一名被敲诈的客人报案。 <br />
<br />
去年3月20日，一名男子在阳春白雪洗浴中心消费后，被要求买单1.3万余元。这名男子随即报警。警方调查发现，该洗浴中心利用色情消费实施敲诈，同时还存在恐吓等行为。 <br />
<br />
侦查员随后到涉案场所秘密侦查，发现王爱国经营的海上大都会、阳春白雪等5处洗浴场所均涉嫌敲诈勒索、组织卖淫嫖娼等违法犯罪行为。警方决定于去年4月9日晚9时许统一实施抓捕。对于这次抓捕，崔大力事先知道，但这一次，他的通风报信已不起作用。 <br />
<br />
据阳春白雪的女经理说，去年4月9日下午6点多，孙美玉给她发短信：“今天晚上停活，以按摩为主，凌晨两点以后再接活。”一小时后，崔大力也给她打电话：“今天晚上分局的民警都没下班，在分局待命，有行动。”当晚9时许，大批警察赶到把店里的小姐抓了，正在洗浴中心对面吃羊肉串的她随后也被抓走。 <br />
<br />
事后，这名经理得知，警方这次是多个警种配合、统一行动，将王爱国在崇文、东城、朝阳三个区的洗浴中心同时包围，控制了以王爱国为首的10名团伙骨干及140余名涉案人员，并将洗浴场所依法查封。 <br />
<br />
没过多久，崔大力因涉嫌受贿也被查获归案。<br />
<br />
6 54人被公诉<br />
<br />
记者了解到，在警方抓捕行动中落网的150余人中，小姐等多数人被劳动教养或被处以行政拘留。经审查，最终有54名被告人于今年3月被公诉至北京市二中院，崔大力则被另案处理。 <br />
<br />
检方指控称，王爱国、孙美玉集团自2003年，先后独立或与他人合股开办或承包阳春白雪等5家洗浴场所，王爱国、孙美玉、王思嘉犯罪集团以上述洗浴场所的经营作掩护，通过各店经理，分别在上述各场所内，组织、领导众多的卖淫人员从事卖淫活动，并为卖淫活动的顺利进行提供保障。 <br />
<br />
截至2009年4月9日，王爱国一家三口指使并伙同各店经理等人，组织卖淫3万次以上，并采用多种方式向嫖客索要高额嫖资，卖淫获利总金额达3000万元以上。据了解，王爱国一家三口指使或授意阳春白雪洗浴中心的经理等人敲诈勒索嫖客钱财，去年3月18日和20日，共敲诈3名嫖客共计49200元。 <br />
<br />
检方认为，王爱国、孙美玉、王思嘉涉嫌组织卖淫罪、敲诈勒索罪；5个洗浴店的经理涉嫌组织卖淫罪；其他人员包括领班、财务、保安、服务生等，则涉嫌协助组织卖淫罪。王爱国因向崔大力行贿轿车，还涉嫌行贿罪。 <br />
<br />
上述案件目前均在审理过程中，不日将宣判。<br />
<br />
<br />
卖淫集团敛财内幕揭秘： 收买上千的哥招揽客源 专门培训小姐对付警察<br />
<br />
<br />
王爱国集团经营的洗浴中心为小姐们提供了赚钱平台，同时也为该集团谋取巨额利益创造了机会。为了保证这一财路不被斩断，他们费尽心机设计了一整套严密的“操作规范”。<br />
<br />
■“管理方式”<br />
<br />
卖淫收入占一大半 <br />
<br />
据洗浴店的财务人员供述，洗浴店的收入至少有一半来自小姐卖淫。这一句话，道出了王爱国集团铤而走险从事卖淫活动的直接动机。 <br />
<br />
洗浴中心经理称，小姐卖淫的价格是王爱国和孙美玉规定的，每次为500元、800元或者1200元，最少不能低于500元。每家店每天接待的嫖客，少的时候两三个，多的时候20个。<br />
<br />
对于卖淫收入，600元以下的，小姐和店里按四六分成，600元以上的部分，小姐拿三成。相比之下，如果小姐给客人做普通保健，最多只能提25元，少的话只有5元到7元。 <br />
<br />
店里的经理和领班等人的工资，多数是固定的，负责全面工作的经理，一个月多的时候可以拿到上万元。 <br />
<br />
为了统一财务制度，孙美玉派王爱国的一个亲戚每天到5家洗浴中心收取流水现金和账单，交给孙美玉。各店没有财务自主权，大一点的开支都要请示孙美玉，孙美玉会安排专人统一购买洗浴店需要的各种用品。 <br />
<br />
对于收上来的账单，孙美玉交给儿子王思嘉统一管理。财务人员称，王思嘉规定，涉及小姐卖淫的账单，计算完员工提成后，三天之内必须撕毁。 <br />
<br />
出租车带客有提成 <br />
<br />
从案卷材料可以看出，王爱国集团经营的5家洗浴场所，多以外地游客为“目标”。孙美玉认为，向本地人高收费容易惹事，外地人则人生地不熟，自己做了不光彩的事也不好声张，相对安全。而这些外地客源，则主要由出租车司机带来，司机会得到提成。 <br />
<br />
最早的时候，孙美玉让手下专门去北京站、西客站等出租车多的地方发小广告，上面有5家洗浴店的地址和联系方式。当司机送来客人，门口保安会给司机发提成卡，司机拿卡领钱。按规矩，司机拉来一名男客人，给70或80元，拉两个男客给100或110元，即每多一名男客人加30元。而拉一名女客人或者多名女客人则只给30元。 <br />
<br />
同时，保安会把送客司机的手机号码一一记下，每隔一段时间，会给他们群发短信，有时温馨提示他们“天热注意多喝水，天冷注意防寒保暖”；有时称店内搞酬宾活动，路过洗浴中心可以领毛巾、玻璃水和充气枕等赠品，当时带客人来的，再送水杯和毛巾。 <br />
<br />
他们还给出租司机发过积分卡，攒够3张，可以换一个保温杯；攒够10张，可以到店里免费享受“一条龙”服务，目的就是让司机多帮助招揽客人。 <br />
<br />
据王思嘉的司机讲，日积月累，他们掌握的司机电话有上千个。这些司机为了赚取提成，纷纷将外地来京乘客拉到洗浴中心，基本保证了店里的客源。 <br />
<br />
客人分级服务有别 <br />
<br />
洗浴店会把客人编号，目的是分出哪些客人可以多收钱。比如，出租司机送来的客人称为16，黑车拉来的称为11，三轮车送来的称为13，自己来的则称为17。对于不同代号的客人，为他们准备的手牌也不同，比如16是蓝色牌，17则是红色牌。 <br />
<br />
据洗浴店多名经理和领班供述，16号是他们多收费的目标，会享受比其他客人更“周到”的服务。 <br />
<br />
出租司机送来客人后，保安会通过电台喊话：“来客人了，16”。很快，前台便拿着蓝色手牌过来接待，并介绍价目表，这期间，前台会了解客人是否抠门、哪里人等情况，然后拿电台喊“客人不错，南方人，来北京出差的，可能是做按摩的”。 <br />
<br />
进入更衣室，服务生给客人开柜子挂衣服时，会继续探听客人情况，认为有钱的，就用电台喊“客人钱包挺厚的”，意思就是客人有钱可以“切”。 <br />
<br />
从16号客人踏进洗浴中心大门开始，保安、前台、领班、各区服务生都要观察，每个环节都用电台报。即便在客人搓澡时，搓澡工都要和客人聊天打探情况，因为搓澡工没有电台，他们会把发现的情况告诉服务生。<br />
<br />
经过一系列“摸底调查”后，服务生基本可以判断客人是休息、做按摩还是嫖娼。确定嫖娼后，他们把客人送进二楼包房，服务生叫小姐让客人挑，挑好后由小姐负责谈价，对于有钱的客人，小姐会把价钱往高了报。 <br />
<br />
■“安全保障”<br />
<br />
设计暗语防患未然<br />
<br />
客人进包房嫖娼的时间，是洗浴中心赚钱最多的时间，同时也是店里最紧张的时候。 <br />
<br />
为了保证卖淫嫖娼不被查，店里基本每人都配一部电台，加强沟通，互相配合。二楼有客人嫖娼的时候，服务生会通过电台通知门口的保安加强防范。为此，他们还设计了一套暗语。 <br />
<br />
比如，客人嫖娼时，服务生会用电台喊“11买单”，保安要回话“收到，前后一切正常”，之后会注意放风。嫖娼结束后，服务生通过电台再喊“11买单OK”，保安回答“收到”，就不用再看着了。 <br />
<br />
根据店经理的供述，孙美玉还会要求他们经常把暗语换换，比如把“11买单”换成“二楼点烟”或者“后边有做按摩的”，相对应的结束语则换为“点烟结束”和“按摩结束”。 <br />
<br />
此外，为了应对警察的突然到访，孙美玉还要求各店安装报警灯，包括保安室、收银台、包房都连着，如果有警车或者警察赶到，保安、收银都可以踩一下报警灯，提醒卖淫的人赶紧收工。 <br />
<br />
嘱咐小姐死不认账<br />
<br />
尽管有一系列的防护措施，洗浴店的小姐还是被抓过现行。 <br />
<br />
据孙美玉交待，2009年3月底，他们在东城区刚开业的一家海港洗浴被警方检查，当时店里的小姐刚脱了衣服，警察就来了。不过，小姐到派出所后死活不承认卖淫，坚持说是和客人处对象，派出所最后放人。 <br />
<br />
这件事后，孙美玉专门组织几家店的经理和领班以吃饭的名义开会，让他们交流经验，如何应对检查，以及小姐被抓后如何编谎话不承认卖淫。 <br />
<br />
饭桌上，孙美玉告诉领班回去给小姐传达，出了事一定要扛住。“警察讯问也就是24小时，你不认，到时间就放人，要是承认了就要劳教半年。” <br />
<br />
其间，有个领班出主意说，如果被警察查到了，小姐就用指甲抓伤自己的胳膊，说客人要强奸，或者让小姐说“客人是搞绘画艺术的，看我不错可以当人体模特，就看看我的身材，我这才脱的衣服”。 <br />
<br />
最后，孙美玉嘱咐，店里小姐统一发的粉色大褂，一定要随时带着，警察如果忽然造访，要赶紧把大褂穿上，总之要避免抓现行。 <br />
<br />
内部规定绝不惹事<br />
<br />
嫖客被高收费后，总会有不愿意交钱的时候。<br />
<br />
对于这种情况，孙美玉规定：“即使不做生意也不能惹事”。她告诫各店经理，遇到客人认为收费高不结账的，“绝对不能发生冲突，不能与客人打架，但可以有言语威胁。” <br />
<br />
一般情况下，嫖娼结束后，小姐会给客人开收费单子，单子有两联，一联要交给前台收银员，一联自己收好等着拿提成。 <br />
<br />
因为各种原因，有的客人不愿签字，并对高额的嫖资提出质疑。这时，小姐会叫包房服务员进去和客人解释，服务员解释不了的，会叫领班解释。实在解释不清的，就由前台领班把客人带进吧台边的一间小屋谈，几个人一起吓唬。 <br />
<br />
有洗浴店经理说，一般情况下，他们一吓唬，客人就交钱了。如果客人钱没带够，服务生会跟着客人去银行取。 <br />
<br />
但也有不交的，对于这种情况，他们为了不让客人报警，就视情况给客人办优惠卡打折，打折到对方满意为止。唯一的一次，他们没能让客人满意，那就是2009年3月20日，被收费1.3万余元的客人因被几个服务生围着不让走，于是报警。这件事是导致王爱国集团被端的直接原因。</div>

							
							
							
							
