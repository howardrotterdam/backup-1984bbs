&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;[导读]该集团猖獗6年未被查获，除自身谨小慎微外，更关键的是警察为其通风报信。作为交换，该民警可以去洗浴中心免费嫖娼。集团有整套严密的“操作规范”，收买上千的哥招揽客源。 (博讯 boxun.com)<br />
<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 近日，一起特大卖淫集团案在北京市二中院审理，集团头目王爱国一家三口被控在6年间开办或承包5家洗浴场所，组织、领导众多卖淫人员从事卖淫3万次以上，获利3000万元以上，集团54名被告人被公诉。 <br />
&nbsp; &nbsp; 案件材料显示，该集团之所以猖獗6年未被查获，除自身的谨小慎微外，更为关键的是有一名警察为他们“放哨”。每当警方检查前，这名民警都会为卖淫团伙通风报信。作为交换，该民警可以去洗浴中心免费嫖娼，并涉嫌收受集团老板王爱国给予的一辆价值7.15万元的轿车。上周，这名警察涉嫌受贿罪被公诉。<br />
&nbsp; &nbsp; 1 结识查店警察<br />
&nbsp; &nbsp; 一手经营起5家洗浴场所的老板王爱国是北京人，现年54岁，初中毕业后曾在北京汽车制造厂工作，1985年辞职后经商，开过歌厅和啤酒屋。 <br />
&nbsp; &nbsp; 2003年，王爱国和妻子孙美玉开始经营洗浴中心，其中有他们全资的，也有与人合股的。 <br />
&nbsp; &nbsp; 据孙美玉供述，他们经营的每家洗浴中心都设有卖淫场所。2003年9月，因其中一家被查出有卖淫嫖娼人员，店里两名领班还被朝阳法院以容留卖淫罪各判处有期徒刑两年。但为了招揽生意、多挣钱，他们一直允许卖淫行为存在，而如何防止被警察查获，成为一个棘手的问题。 <br />
&nbsp; &nbsp; 2007年上半年，崇文公安分局民警崔大力(化名)和队里的同事去崇文区法华寺南里的北京阳春白雪洗浴中心检查工作时，认识了该洗浴中心老板王爱国。当时，王爱国还经营着东城区的金来宝浴池、朝阳区的海上大都会洗浴、崇文区的黄河京都大酒店洗浴部、朝阳区的海日裕阳国际休闲会所。 <br />
&nbsp; &nbsp; 崔大力现年46岁，河北省香河县人，大专文化程度，案发前任北京市公安局崇文分局民警，主任科员，一级警督。据崔大力自述，他早年在部队服役，之后一直在崇文公安分局工作，先后在前门大街派出所、分局治安处任职。2007年8月，他开始在崇文公安分局治安支队行业场所管理队任内勤民警。<br />
&nbsp; &nbsp; 2 全程消费免单<br />
&nbsp; &nbsp; 认识崔大力后，王爱国开始请崔大力吃饭，并提议让自己的儿子王思嘉认崔大力做干爹。现年25岁的王思嘉曾到英国阿伯丁大学留学。认识崔大力时，他刚毕业回国一年，没有另外找工作，而是帮父母经营洗浴中心。 <br />
&nbsp; &nbsp; 为了和崔大力处好关系，王爱国和王思嘉在崔大力去阳春白雪洗浴中心消费时，告诉经理全部免单。在和经理介绍崔大力时，王爱国直言：“这是分局的崔哥，以后崔哥来了好好照顾。” <br />
&nbsp; &nbsp; 领会老板的意图后，这名女经理大胆问崔大力：“崔哥，要不要给你安排一个？”“行，安排一个吧”。就这样，原本负责查处卖淫嫖娼的民警，自己却开始嫖娼。 <br />
&nbsp; &nbsp; 崔大力供述，此后，他每次去阳春白雪，经理都会主动给他安排小姐。最多的时候，他一周嫖娼三四次。收银员开出的支出凭单上注明“招待崔哥”，然后由小姐签字，店里会给小姐提成50元。 <br />
&nbsp; &nbsp; 为了把崔哥照顾好，经理还特意向店里的“妈咪”、小姐透露崔大力的身份，结果一名小姐告诉经理说，崔哥嫖娼时已介绍自己是警察。 <br />
&nbsp; &nbsp; 此外，崔大力还带朋友去嫖娼，他自己不给钱。有时候，他的朋友会给每个小姐300元小费。<br />
&nbsp; &nbsp; 3 警察成线人<br />
&nbsp; &nbsp; 实际上，崔大力在洗浴中心并不是真的受欢迎。阳春白雪的经理向警方供述，他们并不爱接待崔大力，“他一去，我们店里都围着他转，他又不给钱”。而他们之所以讨好崔，就是因为他的特殊身份。“他是警察，能在检查之前给我们一些消息。” <br />
&nbsp; &nbsp; 该经理介绍，崇文公安分局经常去店里检查。有时，老板王爱国、王思嘉事前会给她打电话，告诉她“这两天有检查的，注意点”，这样，他们在接活时就要小心，如果老板让停活，他们就停。这名经理说：“王爱国私下说过，他自己有关系、有人。孙美玉开会时也说，我们要进去了就扛住别说，他们可以找人捞，要是承认了，就没法儿捞了。”崔大力能事先告诉他们消息，为他们从事卖淫活动壮了胆。 <br />
&nbsp; &nbsp; 对此，崔大力在接受讯问时承认自己为洗浴中心通风报信。他说，一般市公安局统一行动或者各区公安分局互查时，他会提前知道，并给阳春白雪的经理打电话，“告诉她今天要检查，让她小心些别出事”。 <br />
&nbsp; &nbsp; 对于自己给他们通风报信的原因，崔大力总结说：“从我个人角度而言，我怕市局检查时把小姐抓了，小姐把我供出来，对我不利；从王爱国的角度而言，我就是他的眼线，一有大规模检查，我就通知他们，不容易出事。”<br />
&nbsp; &nbsp; 4 受贿捷达轿车<br />
&nbsp; &nbsp; 除了给王爱国通风报信，崔大力还帮王爱国联系过一桩承包宾馆客房的生意，并涉嫌收受王爱国给予的一辆价值7.15万元的捷达轿车。 <br />
&nbsp; &nbsp; 崔大力说，2008年10月，他的朋友张某和他说，宝鼎大厦有80间客房要对外承包，租金便宜，可以合作承租来赚钱。“我也想挣点钱，提高生活质量”，于是，他将这桩生意介绍给了有资金实力的王爱国，后者成功将客房承包。 <br />
&nbsp; &nbsp; 当时，王爱国对张某说：“老崔给介绍的，他也挺苦的，给他5%的干股，但不用出钱，什么都不用他负责。”崔大力对此默认，但出于谨慎，他没有在股东协议上签字。 <br />
&nbsp; &nbsp; 王爱国供述，2008年底，崔大力几次向他提出自己没有车，到哪里办事都不方便。“当时我就明白了，崔大力是让我给他买辆车。” <br />
&nbsp; &nbsp; 王爱国说，考虑到自己的买卖在崔大力的地面上，得罪不起他，又需要他罩着，同时他又给自己联系了生意，于是就问他想买什么车。当听到后者就想买辆老款捷达时，他觉得要求不高，爽快答应了。去年3月26日，王爱国和崔大力一起去买了辆7.15万元的捷达车。 <br />
&nbsp; &nbsp; 对于王爱国送车一事，崔大力不否认，但他称那是王爱国看他没车，主动以宾馆提前分红的名义给他买的。 <br />
&nbsp; &nbsp; 不过，崔大力最后还是承认自己涉嫌受贿，“王爱国给我干股、送车，让我去洗浴中心消费免单，都是利用我这个人民警察手中的职权，给他站岗、通风报信，使他逃避法律的制裁”。<br />
&nbsp; &nbsp; 5 顾客被敲诈报警<br />
&nbsp; &nbsp; 尽管有崔大力为之通风报信，但王爱国的洗浴中心最终还是难逃法网。直接导致警方出手的，是一名被敲诈的客人报案。 <br />
&nbsp; &nbsp; 去年3月20日，一名男子在阳春白雪洗浴中心消费后，被要求买单1.3万余元。这名男子随即报警。警方调查发现，该洗浴中心利用色情消费实施敲诈，同时还存在恐吓等行为。 <br />
&nbsp; &nbsp; 侦查员随后到涉案场所秘密侦查，发现王爱国经营的海上大都会、阳春白雪等5处洗浴场所均涉嫌敲诈勒索、组织卖淫嫖娼等违法犯罪行为。警方决定于去年4月9日晚9时许统一实施抓捕。对于这次抓捕，崔大力事先知道，但这一次，他的通风报信已不起作用。 <br />
&nbsp; &nbsp; 据阳春白雪的女经理说，去年4月9日下午6点多，孙美玉给她发短信：“今天晚上停活，以按摩为主，凌晨两点以后再接活。”一小时后，崔大力也给她打电话：“今天晚上分局的民警都没下班，在分局待命，有行动。”当晚9时许，大批警察赶到把店里的小姐抓了，正在洗浴中心对面吃羊肉串的她随后也被抓走。 <br />
&nbsp; &nbsp; 事后，这名经理得知，警方这次是多个警种配合、统一行动，将王爱国在崇文、东城、朝阳三个区的洗浴中心同时包围，控制了以王爱国为首的10名团伙骨干及140余名涉案人员，并将洗浴场所依法查封。 <br />
&nbsp; &nbsp; 没过多久，崔大力因涉嫌受贿也被查获归案。<br />
&nbsp; &nbsp; 6 54人被公诉<br />
&nbsp; &nbsp; 记者了解到，在警方抓捕行动中落网的150余人中，小姐等多数人被劳动教养或被处以行政拘留。经审查，最终有54名被告人于今年3月被公诉至北京市二中院，崔大力则被另案处理。 <br />
&nbsp; &nbsp; 检方指控称，王爱国、孙美玉集团自2003年，先后独立或与他人合股开办或承包阳春白雪等5家洗浴场所，王爱国、孙美玉、王思嘉犯罪集团以上述洗浴场所的经营作掩护，通过各店经理，分别在上述各场所内，组织、领导众多的卖淫人员从事卖淫活动，并为卖淫活动的顺利进行提供保障。 <br />
&nbsp; &nbsp; 截至2009年4月9日，王爱国一家三口指使并伙同各店经理等人，组织卖淫3万次以上，并采用多种方式向嫖客索要高额嫖资，卖淫获利总金额达3000万元以上。据了解，王爱国一家三口指使或授意阳春白雪洗浴中心的经理等人敲诈勒索嫖客钱财，去年3月18日和20日，共敲诈3名嫖客共计49200元。 <br />
&nbsp; &nbsp; 检方认为，王爱国、孙美玉、王思嘉涉嫌组织卖淫罪、敲诈勒索罪；5个洗浴店的经理涉嫌组织卖淫罪；其他人员包括领班、财务、保安、服务生等，则涉嫌协助组织卖淫罪。王爱国因向崔大力行贿轿车，还涉嫌行贿罪。 <br />
