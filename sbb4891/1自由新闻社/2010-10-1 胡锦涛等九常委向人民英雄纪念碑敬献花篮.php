&nbsp; &nbsp;&nbsp; &nbsp; 庆祝中华人民共和国成立59周年 首都各界向人民英雄纪念碑敬献花篮<br />
<br />
　　胡锦涛吴邦国温家宝贾庆林李长春习近平李克强贺国强周永康出席仪式<br />
<br />
　10月1日上午，党和国家领导人胡锦涛、吴邦国、温家宝、贾庆林、李长春、习近平、李克强、贺国强、周永康等来到天安门广场，出席首都各界向人民英雄纪念碑敬献花篮仪式。这是胡锦涛等同首都各界代表一起缓步走向人民英雄纪念碑。<br />
　　<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;10月1日上午，党和国家领导人胡锦涛、吴邦国、温家宝、贾庆林、李长春、习近平、李克强、贺国强、周永康等来到天安门广场，出席首都各界向人民英雄纪念碑敬献花篮仪式。这是胡锦涛等在人民英雄纪念碑基座前，深切缅怀革命先烈。<br />
<br />
　　鲜花敬献英灵，伟业告慰先烈。今天是中华人民共和国成立５９周年纪念日。今天上午，党和国家领导人胡锦涛、吴邦国、温家宝、贾庆林、李长春、习近平、李克强、贺国强、周永康等，同首都各界代表一起来到天安门广场，向人民英雄纪念碑敬献花篮，深切缅怀为实现民族独立、人民解放和国家富强、人民幸福英勇献身的革命先烈。<br />
<br />
　　节日的天安门广场上，鲜艳的五星红旗高高飘扬，人民英雄纪念碑巍然耸立，“热烈庆祝中华人民共和国成立五十九周年”、 “坚持改革开放夺取全面建设小康社会新胜利”的大幅标语格外醒目。搭建在广场上的国庆花坛姹紫嫣红，北京奥运会吉祥物、嫦娥奔月工程等模型点缀其间，烘托出喜庆祥和的节日气氛。<br />
<br />
　　今年是改革开放３０周年。以胡锦涛同志为总书记的党中央团结带领全党全国各族人民，深入贯彻党的十七大精神，全面推进党和国家各项事业。北京奥运会、残奥会的成功举办，神舟七号载人航天飞行的英雄壮举，同历史罕见雨雪冰冻灾害和四川汶川特大地震灾害斗争的重大胜利，极大振奋了中华民族精神，显著提高了我国的国际地位。所有这些，使今年的国庆节格外令人感奋和激动。<br />
<br />
　　今天一大早，首都各界代表５０００多人从四面八方聚集到天安门广场，前来参加向人民英雄纪念碑敬献花篮仪式。他们面向纪念碑，排成１０个整齐的方阵。<br />
<br />
　　９时５７分，胡锦涛、吴邦国、温家宝、贾庆林、李长春、习近平、李克强、贺国强、周永康等党和国家领导人步行来到各界代表方阵前，出席向人民英雄纪念碑敬献花篮仪式。<br />
<br />
　　１０时整，仪式正式开始。１８名陆海空三军礼兵迈着整齐有力的步伐，分两列行进到纪念碑前持枪肃立。<br />
<br />
　　这时，军乐队奏响中华人民共和国国歌，雄壮的国歌声在广场上空回荡。<br />
<br />
　　随后，全场肃立，向为中国人民解放事业和共和国建设事业英勇献身的烈士们默哀。<br />
<br />
　　中共中央，全国人大常委会，国务院，全国政协，中央军委，各民主党派、全国工商联和无党派爱国人士，各人民团体，首都各界群众，中国少年先锋队分别敬献的９个大型花篮，一字排开地摆放在人民英雄纪念碑北侧。一个个花篮的红色缎带上写着“人民英雄永垂不朽”８个金色大字，一朵朵盛开的鲜花散发出缕缕清香，寄托着对革命先烈的无限敬仰和无尽怀念。<br />
<br />
　　在军乐队演奏的激昂深情的《献花曲》旋律中，１８名礼兵抬起９个花篮，正步走向人民英雄纪念碑，将花篮摆放到纪念碑基座前。<br />
<br />
　　胡锦涛等党和国家领导人缓步登上纪念碑基座，在花篮前驻足凝视。<br />
<br />
　　胡锦涛神情庄重地走近花篮，仔细整理花篮上的红色缎带。<br />
<br />
　　接着，胡锦涛等党和国家领导人绕行一周，深情瞻仰人民英雄纪念碑。碑座四周的大型浮雕，记载着近代以来中国人民顽强不屈的奋斗历程。胡锦涛等边走边看，向人民英雄致以深深的敬意。<br />
<br />
　　伴随着动人的乐曲声，首都各界代表排成纵队，依次登上纪念碑基座。他们当中，有身经百战的老战士代表，有胸佩奖章的劳动模范代表，有为成功举办北京奥运会、残奥会做出突出贡献的先进集体和先进个人代表，有来自基层的工人、农民、科技工作者、大中学校师生代表，有军容严整的人民解放军和武警部队官兵，有身着鲜艳服装的少数民族代表，有戴着红领巾的少年儿童……在这个庄严的时刻，人们怀着崇敬的心情瞻仰人民英雄纪念碑，共同缅怀革命先烈的丰功伟绩。<br />
<br />
　　敬献花篮仪式由中共中央政治局委员、北京市委书记刘淇主持。<br />
<br />
　　参加仪式的领导同志还有：王刚、王兆国、王岐山、回良玉、刘云山、刘延东、李源潮、张德江、徐才厚、郭伯雄、何勇、令计划、王沪宁、乌云其木格、周铁农、李建国、陈昌智、梁光烈、马凯、孟建柱、戴秉国、王胜俊、廖晖、白立忱、陈奎元、李兆焯、黄孟复、张梅颖、张榕明、钱运录、孙家正、李金华、郑万通、万钢、林文漪、罗富和，中央军委委员陈炳德、李继耐、廖锡龙、常万全、靖志远、吴胜利、许其亮。<br />
<br />
　　中央党政军群有关部门和北京市主要负责人，各民主党派中央、全国工商联负责人和无党派人士代表，也参加了敬献花篮仪式。<br />
