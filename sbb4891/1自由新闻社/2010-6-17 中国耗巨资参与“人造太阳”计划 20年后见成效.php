“人造太阳”计划（资料图片）<br />
<br />
<img src="http://img1.gtimg.com/news/pics/hv1/236/107/549/35726246.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
热核聚变试验堆模拟图（资料图片）<br />
<br />
中广网南京6月17日消息 据中国之声《新闻纵横》07时13分报道，看过灾难大片《2012》的朋友可能还记得，影片里有一个庞大的、多国合作完成的“诺亚方舟”计划，承载着人类继续繁衍生存的希望。但是您相信吗？现实中，国际间确实有一个多国合作计划，只不过在这个计划中，多国合作建设的不是“诺亚方舟”，而是被称为&quot;人造太阳&quot;的国际热核聚变实验堆，它承载的是人类未来新能源的希望。<br />
<br />
昨天（16日），国际热核聚变实验堆7个成员国代表齐聚中国江苏，共同研究这一可能影响人类从根本上解决能源问题的计划。那么，目前这一计划进展的怎么样？遇到哪些目前无法解决的难题呢？马上连线中国之声前方记者费磊，了解详细情况。<br />
<br />
主持人：目前被称为人造太阳计划的国际热核聚变实验堆计划进展怎么样了？<br />
<br />
记者：人造太阳计划就是要复制太阳内部的核聚变的过程产生巨大的能量供人类使用，早在上世纪50年代，人类就实现了核聚变的反应就是氢弹的爆炸，但是氢弹瞬间猛烈爆炸是无法控制的，所以人们就想要把氢弹爆炸出的巨大能量如何来能够被人类用于社会生产和人类生活，伊特尔计划一提出就受到了世界各国政府的青睐，到目前伊特尔计划总共有七个方面，三十三个国家参与这个计划。<br />
<br />
总的来说，中国、欧盟、日本、韩国、俄罗斯、美国和印度，这33个国家计划涉及到全球60%的人口和80%的GDP，是个相当宏大的计划，它也成为了世界上最大的一个国际大科学工程化，伊特尔集成了当今世界上受控磁约束核聚变的主要的科学和技术成果，从2008年开始，伊特尔试验堆工程已经在法国南部城市卡达拉舍进行建设，项目预期要持续30年，10年作为前的工程期建设，后20年作为试验运行期。<br />
<br />
预算总经费是100亿欧元，其中欧盟承担40%的经费份额，其他六方各承担9%，这次第六届理事会就是要审议伊特尔一个最基本的计划叫计划基准，也就是对伊特尔计划的总进度、总费用、技术规格、项目管理在内的几个文件进行最后的审核，然后就要开始在法国南部城市进行具体的核聚变试验堆的工程建设了。<br />
<br />
在昨天（16日）的会议开幕式上，科技部部长万钢也表示，中国方面也将会一如既往的支持伊特尔组织的工作，履行中方在伊特尔计划中的各项承诺和义务，如果说最终大家要看到，核聚变实验堆能够完成它的实验供人类生活大概也要得20年到30年以后才能见到最初的成效。<br />
