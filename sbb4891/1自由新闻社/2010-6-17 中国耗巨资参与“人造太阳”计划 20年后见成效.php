人造太阳面临众多难题 期待破解任重而道远<br />
<br />
国际热核聚变实验堆计划是目前世界上仅次于国际空间站的国际大科学工程计划，是人类受控核聚变研究走向实用的关键一步，因此备受各国政府与科技界的高度重视和支持。<br />
<br />
人类的“人造太阳”计划还要多久才能成功，并为人类造福呢？中国能源网首席信息官韩晓平昨晚在接受中国之声《新闻纵横》值班编辑采访时说，目前这一工程还面临很多难以解决的困难，离人类最后的实际利用还有很长的路要走。<br />
<br />
中广网北京6月17日消息 据中国之声《新闻纵横》07时15分报道，对于人类和平利用核能，我们熟悉的是现在各国普遍采用的核电站，它是靠核裂变产生的能量发电，而国际热核聚变实验堆则是靠核聚变获取能量，利用聚变获取能量将更加环保、可利用资源无限大、还不会产生放射性废料。<br />
<br />
另外，国际热核聚变实验堆的反应原理与太阳内部的聚变原理一样，所以它也被人们形象的成为“人造太阳”。科技部副部长曹建林在接受中国之声采访时说，“人造太阳”计划将帮助人们彻底摆脱能源短缺的困境：<br />
<br />
曹建林：在太阳内部不断的进行着核聚变，然后它放出巨大的能量，如果我们能在地球上造成这种核聚变，实际上就是直接在造太阳，模拟太阳能源的制造机理的话制造能源，将比较彻底的解决人类所面临的能源问题。<br />
<br />
中国核工业集团公司核聚变专家段旭如在接受我们采访时说，从理论上看，国际热核聚变实验堆几乎能为人类提供无穷的能源。<br />
<br />
段旭如：大家知道现在我们这个能源需求越来越大，现在对于我们国内来说，现在的主要能源是化学能源，也就是煤和天然气、石油，现在这些能源的消耗和使用会产生很多的一些问题，但是未来一个不能满足我们的需求。<br />
<br />
另外对环境造成很大的影响，核聚变实际上它有很多的优点，一个是资源可以说是无限的，它主要的是新的同位素氘和氚以深海里面提取的氘，若全部反应掉，释放出的能量相当于300升汽油提供的能量，可想而知对我们未来的发展是很关键的。<br />
<br />
国际热核聚变实验堆计划是目前世界上仅次于国际空间站的国际大科学工程计划，是人类受控核聚变研究走向实用的关键一步，因此备受各国政府与科技界的高度重视和支持。<br />
<br />
1985年，正处在冷战时期的美、苏两个核超级大国在日内瓦峰会上提出了一个人类历史上前所未有的宏大合作计划――“国际热核聚变实验堆计划”，这就是被称作“伊特尔”的人造太阳计划。1988年“国际热核聚变实验堆”计划正式启动。 经过20多年的共同努力，在克服一个又一个重大科研难题的基础上，由中、欧、日、韩、俄、美六方组成的ITER国际组织于2007年10月24号正式成立，后来印度也加入进来。<br />
<br />
如今，正在法国南部城市卡达拉舍建设的ITER工程是目前世界上规模最大的国际科研合作项目之一。国际热核聚变实验堆项目预期持续30年，10年作为工程建设，20年作为运行，最初估计总经费100亿欧元，其中欧盟承担40％，其余6方各承担9％。<br />
<br />
中国在2003年2月宣布作为全权独立成员加入该计划谈判。这意味着中国承诺承担国际热核聚变实验堆工程总造价100亿欧元的10%，并享受全部知识产权。中国核工业集团公司核聚变专家段旭如介绍说，中国参与这个项目意义重大。<br />
<br />
段旭如：欧盟它承担的义务和经费的话，差不多50%，其他几方就是9%或者说10%，中国也提供9%到10%的支持，这个支持的话，70%以上是提供实物的贡献，也就是说给衣特尔要提供这些部件，全部的知识产权我们可以共享，这样的话我们虽然只承担了10%，但实际上其他90%我们有很多的一些技术资料都可以共享的，这样的话对今后我们建造自己的试验堆有更多技术上的支持。<br />
