&quot;When fame happens, people get carried away, right? The Dalai Lama, despite tremendous adoration as well as adulation ... is very conscious of that,&quot; Tethong said. &quot;One of the Buddhist practices is to always be very aware of one's self and how one looks at one's self and not to be carried away with one's ego.&quot;<br />
<br />
Not standing on formalities -- he playfully threw snow at reporters outside the White House on Thursday -- staying grounded and his constant ability to exude warmth and joy have made him easy to love, people who admire him say.<br />
<br />
&quot;He really is the real deal -- a truly loveable guy. He lives his values,&quot; said Jamie Metzl, executive vice president of the Asia Society, a global organization that seeks to increase understanding and relationships between the U.S. and Asia. &quot;Recognizing someone who lives their life according to such positive principles helps us all grow.&quot;<br />
<br />
And Metzl, who said he's met the Dalai Lama three times, suggested the Chinese government, through its denunciation of the spiritual leader, has bolstered his recognition. He said that by saying the Dalai Lama is &quot;a wolf in sheep's clothing,&quot; a claim Metzl said doesn't match what people read and see, &quot;the Chinese are doing a great deal to turn him into a rock star.&quot;<br />
<br />
But nothing does more to make people appreciate the Dalai Lama than being with him, said Charles Raison, a psychiatrist with Emory University Medical School.<br />
<br />
Raison, who's been involved in a program where Western doctors work with and exchange teachings with Buddhist monks, recounted a time when he, his wife and several others met with the Dalai Lama about four years ago.<br />
<br />
&quot;Many people, myself included, have a powerful experience in his presence. I nearly erupted in tears,&quot; he said. And his wife, whom he said &quot;does not have a religious bone in her body&quot; was &quot;just beaming.&quot;<br />
<br />
He said studies have long shown that people have a physiological response to the behaviors, feelings and even smells put forth by others.<br />
<br />
&quot;Buddhists,&quot; he added, &quot;say that sweet smells come from a saint -- a mark of spiritual advancement.&quot;<br />
<br />
And given the Dalai Lama's effect, his smile, his laughter, his sense of peace and gentle spirit, it's no wonder people fall for him. Even if they haven't had the chance to meet him. <br />
