													<h2>Why Americans love the Dalai Lama</h2>
						
						
																											<div id="postmessage_489572" class="t_msgfont"><a href="http://www.cnn.com/2010/LIVING/02/22/americans.love.dalai.lama/index.html?iref=allsearch" target="_blank">http://www.cnn.com/2010/LIVING/0 ... html?iref=allsearch</a><br />
<br />
<img src="http://i.cdn.turner.com/cnn/2010/LIVING/02/22/americans.love.dalai.lama/t1larg.dalai.lama.gi.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
On CNN on Monday, the Dalai Lama goes one-on-one with Larry King in his first interview after his controversial meeting with President Obama. Hear his thoughts about China, human rights and the situation in Haiti. Monday night, 9 ET on &quot;Larry King Live.&quot; <br />
<br />
(CNN) -- He's been decorated with awards and called one of the world's most influential people. He's addressed packed auditoriums and waved to crowds who line streets just to catch a passing glimpse of him. He's shaken the hands of countless global dignitaries and earned a fan base following on Facebook that might rival that of Hollywood stars.<br />
<br />
He is His Holiness the 14th Dalai Lama, the 74-year-old spiritual leader of Tibet and the head of the Tibetan government-in-exile, based in Dharamsala, India. And though he describes himself, according to his Web site, as &quot;a simple Buddhist monk,&quot; the love so many Americans and others have for him has, no doubt, bestowed on him iconic status -- whether he sees it that way or not.<br />
<br />
&quot;I'd love to be in his presence. I'd love to be in an audience where he speaks,&quot; said Jerilee Auclair, 55, of Vancouver, Washington, who has yet to have that pleasure. &quot;I yearn for it. I watch his schedule to see if/when he'll be in my area. ... I love what he stands for. His inner peace inspires me to find mine, daily.&quot;<br />
<br />
She's far from alone in her admiration.<br />
<br />
A CNN/Opinion Research Corp. survey released Thursday, the same day the Dalai Lama visited the White House, showed that 56 percent of Americans hold a favorable view of him, putting him &quot;in the same neighborhood as other major religious figures,&quot; said CNN Polling Director Keating Holland. &quot;Favorable ratings for the pope, at 59 percent, and Billy Graham, at 57 percent, are virtually identical.&quot;<br />
<br />
Not bad for a guy who lives on the opposite side of the globe, is entrenched in a decades-old political and cultural struggle many don't understand, and lives according to a tradition few Americans follow. Less than 1 percent of Americans identify themselves as Buddhist, with less than 0.3 percent of those being Tibetan Buddhist, according to The Pew Forum on Religion &amp; Public Life.<br />
<br />
But what he represents resonates with Americans who may need a figure like the Dalai Lama to look to, said Ganden Thurman, executive director of New York City's Tibet House, an organization dedicated to preserving Tibetan culture and civilization.<br />
<br />
&quot;He stands for achieving peace by way of peace, and since Gandhi and Martin Luther King aren't around, he's a placeholder for that kind of position,&quot; he said. &quot;He says he's a 'simple monk,' but that's wishful thinking. He's a monk that's been saddled with the responsibility of shouldering the hopes and dreams of millions of Tibetan people. ... He's doing the best he can with that, and frankly, these are the kind of people we admire.&quot;<br />
<br />
Not that Thurman, 42, always treated the Dalai Lama with this kind of reverence. His father, Robert Thurman, co-founded the Tibet House, is an Indo-Tibetan Buddhist studies professor at Columbia University and holds the first endowed chair in Buddhist studies in the West, according to the university's online biography. The older Thurman, who also happens to be the father of actress Uma Thurman, was a personal student of the Dalai Lama, and it was through this relationship that his son first met the spiritual leader.<br />
<br />
&quot;My earliest memory of meeting him, I was around 4. I was a pretty rambunctious 4-year-old,&quot; he said with a laugh, guessing that he probably jumped on His Holiness and grabbed at the man's glasses. &quot;Diplomatic protocol wasn't high on my list of priorities.&quot;<br />
<br />
<br />
<br />
Video: Dalai Lama on meeting Obama Tenzin Tethong has known the Dalai Lama since he was a child. He worked in the exile government and served as the spiritual leader's representative in New York and Washington during the 1970s and 1980s. Now the president of The Dalai Lama Foundation, a Redwood City, California, organization that promotes peace, Tethong said he organized the Tibetan leader's first visit to the United States in 1979, 20 years after he had gone into exile<br />
<br />
He recalled not being sure they'd be able to pull off the visit because by the early 1970s, the U.S. had normalized its relations with China, which has long viewed the Dalai Lama as a threat to its national unity on the issue of Tibetan autonomy. But they came at the invitation of various colleges and religious groups, and the American fascination with the Dalai Lama -- the curiosity about his exotic past, his beliefs and his teachings -- spoke volumes then, Tethong said.<br />
<br />
In the decades since, the Dalai Lama's star power has only risen as Americans have learned more about his commitment to nonviolence, interfaith outreach and more. For starters, there was that Nobel Peace Prize he won in 1989. <br />
<br />
High-profile supporters, like actor Richard Gere, helped give him and his people's struggles pop culture prominence, as did several mainstream films including &quot;Seven Years in Tibet,&quot; starring Brad Pitt, and &quot;Kundun,&quot; directed by Martin Scorsese. <br />
<br />
With the increased exposure, there has also been a growing prevalence of &quot;Free Tibet&quot; bumper stickers, the appearance of Tibetan prayer flags in suburbia and Facebook fans who shower the Dalai Lama with praise.<br />
<br />
&quot;Have a nice and easy day with Obama! Namaste,&quot; one woman wrote Thursday. &quot;thank you for all your love, guidance and wisdom ... u changed my life,&quot; a man added. And then this from a college-student fan: &quot;HH Dalai Lama!! You kick metaphorical ass!!!&quot;<br />
<br />
How has all this attention not gone to his head?<br />
<br />
He stands for achieving peace by way of peace, and since Gandhi and Martin Luther King aren't around, he's a placeholder for that kind of position. <br />
<br />
--Ganden Thurman, executive director of Tibet House<br />
 <br />
