从四川回来至今，我一直在琢磨媒体对军队等国家机器动员体制的干预可能，到目前为止，这还完全是一个雷区。从映秀发回来的一篇稿子《映秀救援：苦守144小时，爱人离去》中，我小心翼翼极为隐晦地提出了这种干预的必要性。我总觉得，即便当时救援情况紧急，部队调度机制特殊，救援当时不应苛求，但此类问题，相信应得到必要之反思和检讨，获取从技术到机制上的诸多经验教训，方能减少此后种种不必要损失。　　<br />
