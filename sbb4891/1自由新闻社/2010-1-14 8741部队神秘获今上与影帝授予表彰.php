<br />
比灾区人民的坚韧更让我震撼的，是军队救援时的艰苦卓绝。在映秀的五天五夜里，济南军区一只叫铁军的部队，给我留下了极深的印象。这是一支最早挺进灾区也承担着最艰苦的开路先锋任务的部队，他们的士兵是如此年轻而淳朴，几乎个个都是刚刚从乡村洗脚上岸的许三多，我都能闻到他们身上扑面而来的泥土气息。　　<br />
