																											<div id="postmessage_216866" class="t_msgfont">80后成中国购房主力军&nbsp; &nbsp;扛过开发商扛不过新娘子<br />
<br />
2009年08月27日10:06<br />
<br />
<br />
　　郑州的一家咨询机构负责人近日在接受记者采访时说,眼下80后正在成为郑州住房购买的主力军。这位负责人同时补充说,全国好像都是这样。<br />
<br />
　　是好像还是确实呢？记者随即在网上输入“80后购房”几个字,这才发现“80后购房”根本不是什么新鲜话题,而是当下的热门话题。来自各地的调查均显示,80后成为当今中国购房主力军名符其实。<br />
<br />
　　首先我们来看下号称中国“中心”之城的郑州市。据该市房管局最新公布的数据,郑州市今年7月份共销售商品房11529套(间),这一数字是郑州楼市销售的又一高峰。而咨询机构的分析则显示,郑州市7月份所售房屋中有60%是被80后买走的。<br />
<br />
　　中原如此,西南重镇重庆的情况如何呢？据重庆市春季房交会受访者资料,该市购房者的年龄出现非常强烈的“年轻化”趋势,20～30岁的“80后”购房者占到调查总量的60%。<br />
<br />
　　显然这一数字和郑州的调查结果相吻合。<br />
<br />
　　古城西安怎样？当地媒体报道说:据调查,在西安房地产市场中,“80后”客户群体已经占到了相当大的比例,近期,在西安的首创・国际城、水岸东方、橡树街区、华城万象等项目的售楼部,总是可以见到“80后”寻房的身影。<br />
<br />
　　虽然没有西安方面的准确数字,按照当地媒体的说法80后成为古城西安购房主力是没有问题的。<br />
<br />
　　东部城市情况？来自张家口市的咨询机构调查报告说,现今的张家口楼市中,两类购房人群最为活跃,一是四十岁左右的 “事业有成者”,多为追求更高的生活品质而“换房”;二是“80后”人群。据张家口市各楼盘统计显示,在住宅市场中,大多数楼盘年龄在20岁到30岁的年轻客户群体所占比例已经超过半数,有的楼盘甚至超过了70%。<br />
<br />
　　北京市统计信息网数据显示,2007年,北京市20―30岁人口数量为559万,占全市户籍人口1231万总人口数的22%。以此推算,目前北京市20―30岁人口数占北京总人口数的比例也应在22%左右。如果这个推算大体准确的话,相信其他城市80后的人口比例也与此差不多。如此庞大的人群, 自然有住房需求。<br />
<br />
　　问题是,80后经济实力并不雄厚,为什么要那么急于买房呢？<br />
<br />
　　业内人士分析说,上世纪80年代生育高峰时期出生的人口,当下80后逐渐步入结婚年龄,买婚房是80后的刚性需求。而&lt;中国青年报&gt;的调查问卷也显示,因为结婚而准备买房(或已买房)的80后占到了所有80后买房者中的66.8%。<br />
<br />
　　郑州媒体从业人员小王最近刚买了套60平方米左右的新房。他说,结婚怎么能没有新房呢？要真是那样的话,谁都会不高兴的。<br />
<br />
　　河南房地产业商会常务副会长赵进京似乎一语道破了郑州乃至全国80后成为购房主力的原因。他说:“80后买房是目前最大的刚性需求,因为他们可以扛过开发商,但是扛不过新娘子。”<br />
<br />
　　当然,见于80后的经济实力还不那么雄厚,这就注定了80后买房有两个特点:其一不一定都要自己掏腰包,其二所购房屋以中小户型为主。<br />
