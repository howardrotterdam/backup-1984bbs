<br />
<br />
温州网2月4日报道：近日，“校长嫖娼”事件，引发网民热议。“市区某著名学校校长‘双飞’被‘双开’”，温州街头巷尾也纷纷议论。从校长“双飞”，到校长伙同其他两名男子，同时嫖宿两个卖淫女，各种不同版本的传言在网络广为流传。<br />
<br />
　　今天下午，就网上热炒的校长涉嫌嫖娼问题，温州市公安局新闻发言人办公室经向相关办案单位核实，披露个中详情。<br />
<br />
　　网络传言名校校长“双飞”被抓<br />
<br />
　　近日，网络上出现帖文，称市区某著名学校校长协同某局员工以及第三人在市区一娱乐场所找小姐“双飞”时，被警察抓个现场。<br />
<br />
　　在该帖子中，还称“据权威人士透露，小姐交代的细节不堪入目，目前，在主管部门领导斡旋下，该校长已被取保，但知情人士说，该校长‘双开’已成定局。”<br />
<br />
　　一时间，关于这位“校长”身份引发一阵疑云，许多网友纷纷跟帖留言要求“权威人士”公布名单。<br />
<br />
　　随后，有网友跟帖称该校长为市区某著名小学校长“唐某”，更有网友发帖称“广场路小学，你要挺住！”。<br />
<br />
　　此后，这些“爆料”校长召妓被抓的网帖，忽隐忽现，几次被删除，后又重新冒出来。而官方又一直没有发布权威说法，引发网络上更大的一阵议论。<br />
<br />
　　今天下午，记者从温州市公安局获悉，他们已经在处理这一事件，并向媒体通报了这一事件的内幕。<br />
<br />
　　官方回复：已给予治安罚款处理<br />
<br />
　　今天下午，温州市公安局新闻发言人介绍，在2010年01月25日23时许，积极组织参与“冬季行动”的温州市公安局特警支队民警在临检时，在鹿城区小南路84号与“地军海”桑拿中心毗邻的华天池水疗保健按摩院内查获多起卖淫嫖娼活动，并且在现场抓获涉嫌卖淫嫖娼违法嫌疑人唐某某等14人。<br />
<br />
　　经询问后，查明唐某某(温州鹿城区某小学校长)、傅某某(温州鹿城区某局执法大队副大队长)、卢某某(鹿城保安公司驾驶员)等5人在保健按摩院进行淫亵活动，查获时其行为特征符合公安部关于卖淫嫖娼的认定标准。<br />
<br />
　　据此，市公安局根据《治安管理处罚法》，结合具体违法情节，给予治安罚款处理；对现场抓获的卖淫女孙某等6人处以治安拘留处罚；对现场抓获的按摩院组织经营者杨某等3人以涉嫌容留卖淫予以刑事拘留，场所已批准决定停业整顿，由于主要经营参与者已刑拘在案，场所已经自然关闭。目前正在对幕后老板进行深查。<br />
<br />
　　因涉嫌嫖娼的唐某某、傅某某系公职人员，处理决定书已以书面邮寄形式送达有关单位。卢某系临时工，已口头告知雇佣单位负责人。<br />
<br />
　　目前，此案详情，公安、教育等有关部门正在进一步调查中。</div>

							
							
							
							
