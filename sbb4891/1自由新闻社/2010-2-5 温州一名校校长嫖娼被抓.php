													<h2>唐洪象坦言：凭着良心办教育</h2>
						
						
																											<div id="postmessage_468923" class="t_msgfont">教育公平系列报道之三―― 唐洪象坦言：凭着良心办教育<br />
<br />
发布: 2009-7-15 11:25 |&nbsp;&nbsp;作者: 项锐 |&nbsp; &nbsp;来源: 温州视窗 人民网&nbsp;&nbsp;|&nbsp;&nbsp;查看: 50次<br />
&nbsp; &nbsp;&nbsp; &nbsp; 教育公平，在温州市广场路小学校长唐洪象眼中这是个“良心”问题。<br />
<br />
　　创办于清光绪年间的温州市广场路小学，是浙南大地上享有盛誉的历史名校，百年来获得荣誉无数：“全国百所名牌学校”之一、“全国少先队红旗大队”、“全国红领巾示范学校”、“浙江省教育科研工作先进集体”、 “浙江省教育科研200强”之一、“中国名校600家”之一；她还是中央教科所国家教育部“九五”、“十五”重点研究项目实验学校，全国语文“四结合”实验先进单位、全国先进艺教集体、全国体育先进学校、浙江省文明学校、浙江省科技活动先进集体……<br />
<br />
　　“凭着良心去办教育”。这是唐洪象时常念叨着的一句话。在广场路小学，唐洪象要求每位教师必须端正思想，对学生一视同仁，让他们享受均衡的教育资源。如此“才对得起广大老百姓，”唐洪象说。<br />
<br />
　　对于“教育不公平”现象存在的原因，唐洪象从三个方面展开了分析：<br />
<br />
　　一是教育经费投入不足。城乡经济发展不平衡的背景下，各个区域之间教师福利待遇不同，学校硬件设施有别，造成教育资源分配不均衡，这是“教育不公平”的主要原因。<br />
<br />
　　二是教育体制本身存在问题。一直以来中考、高考都用“千军万马过独木桥”来形容，最主要的原因是“一张考卷定终生”。“进入好的学校，就意味着将来会有个好的工作，”唐洪象说，“所以很多家长在子女上幼儿园时，就开始千方百计选择名校。”<br />
<br />
　　三是温州乃至全国的中产阶级阶层已经形成。从计划经济到市场经济，中国形成了一批富人，其自身的发展经历和周边环境的影响，使得他们对于教育有格外深刻的认识和追求，“更何况3、5万元的择校费，对这些富人来说只是九牛一毛。”所以，才造就了择校风越来越猛烈。<br />
<br />
　　唐洪象校长说，早在1997年，国家教委就印发乐《治理中小学乱收费工作的意见》；2000年，教育部办公厅印发《关于全国中小学收费专项治理工作实施意见》，这些文件都欲整治择校费问题，但难见实效。<br />
<br />
　　“国家很早就规定义务教育阶段不能收取择校费，但我们（温州）为什么还在收？”唐洪象的分析是温州“政穷民富”。<br />
<br />
　　“如果没有择校费，教师的三金都不能保证，在温州这块地方，每月一千多的工资，怎么生存？政府只能靠收取择校费来弥补教育投入的不足，”唐洪象说。<br />
<br />
　　唐洪象介绍，择校费是由温州市教育基金会统一收取，其中有30%的资金返回给学校，“我们就靠这些钱来改善学校的硬件设施。”<br />
<br />
　　唐洪象以使用一间多媒体教室的成本来向记者说明办学的窘境，“现在我们学校有60多个班级，上一节多媒体课的成本是36元，如果不靠这些返回的择校费，我们怎么维持运转，难道把多媒体教室都关了？”<br />
<br />
　　“我们现在要呼吁政府真正地把教育摆在优先发展地位。”唐洪象强调了一下“真正地”，“虽然政府年年都这么说，但光说是没用的，你必须要有投入。”</div>

							
							
							
							
