													<h2>李敖：爱台湾就承认它是中国一部分</h2>
						
						
																											<div id="postmessage_802154" class="t_msgfont">李敖台湾馆祈福 爱台湾就承认它是中国一部分<br />
<br />
2010年08月28日07:59<br />
解放网<br />
<br />
<img src="http://img1.gtimg.com/2010/pics/hv1/107/203/611/39782147.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
李敖（左二）与儿子李戡（左一）、女儿李湛（右二）、妻子王小屯在中国国家馆顶部平台上合影<br />
<br />
昨天上午，台湾历史学家、作家李敖携妻子、儿女共游世博园。虽然已届75岁高龄，首次来观博的李敖却兴致极好，马不停蹄看完中国国家馆、上海馆(网上上海馆)、浙江馆(网上浙江馆)等5个展馆，并在台湾馆(网上台湾馆)两度点天灯祈福，均祝愿“两岸和平”。 <br />
<br />
参观省区市馆“哈尔滨人”“沪上归来”<br />
<br />
上午10点，照旧是那身经典的红夹克，李敖被众人簇拥着走进省区市馆。世博首站，李敖选择了上海馆。对于李敖的来访，东道主上海馆显然也是备加重视，特意安排一名六七岁的上海小囡上前迎接，引得李敖心情大好，与小女孩亲切地交谈起来。<br />
<br />
由于安保限制，媒体未能跟随李敖进入馆内参观。不过，上海馆的主展影片似乎再次勾起李敖对上海这座城市的难解情怀。看完影片之后，他在上海馆的留言簿上写道“沪上归来，快何如之”，与前日在机场所题之“旧地重游，阿拉归来”相得益彰。<br />
<br />
随后，李敖一家又参观了浙江馆和黑龙江馆(网上黑龙江馆)。似乎是题词上了瘾，在浙江馆看浙江新貌、品龙井新茶之后，李敖又一次提笔写下“高兴看到没有蒋介石的浙江”，并解释说，在台湾一提起浙江，就会出现蒋介石，“今天终于在这里看到一个没有蒋介石的新浙江，很好”。<br />
<br />
出了浙江馆，对面即是黑龙江馆。看到冰天雪地的北国风光，出生于哈尔滨的李敖有些激动，一进黑龙江馆就握住馆长的手，大呼“老乡”。而在黑龙江馆的留言簿上，李敖也动情地挥就“吾乃哈尔滨人也”，并用已经不太标准的东北话对记者说：“我是哈尔滨人！”<br />
<br />
游中国国家馆“我的红也是中国红”<br />
<br />
中国国家馆是李敖此次世博之行“最想看的馆”。在馆长徐沪滨的亲自引领下，李敖一家先是登上顶部的观景平台，俯瞰世博园全景，而后与普通游客一起观看了主展影片《历程》。8分钟的影片，李敖看得十分仔细，观罢还风趣地说，要给《历程》提两个小意见：“一是地震后，从废墟中伸出的那只手太干净了，应该是沾满尘土才对；二是片子里只有中央电视台，如果加凤凰卫视就更好了。”<br />
<br />
听到徐沪滨介绍说中国国家馆的大红外观参考了故宫古建筑群中的七种红色时，李敖连连点头称好，并笑着指指自己的红夹克问：“我属于哪一种红？”徐沪滨幽默地答道：“你是中国国家馆的第八红。”对于这个“新封号”，李敖甚为满意，后来多次指着自己的红夹克告诉记者：“馆长说了，我的红也是中国红，是中国国家馆的‘第八红’。”<br />
<br />
对于主展区的《清河上河图》和铜车马，历史学出身的李敖表现出浓厚兴趣。特别是动态版《清明上河图》令李敖大为赞叹，参观过程中不时与徐沪滨和随行的凤凰卫视董事局主席刘长乐交谈询问。行至图卷尾端时，他还主动招呼妻子、儿女站在一起，以《清明上河图》为背景拍张“全家福”。李敖表示：“我是学历史的，中国国家馆让我对古往今来了解得更透彻了，我过去的了解，都是文字上的认识，现在的了解，可以变得更加具体。”<br />
<br />
谈及对中国国家馆的整个印象，李敖以“好、壮、美”三个字概括了中国国家馆给自己带来的震撼。他表示，中国国家馆的斗拱造形和大红外观，令人印象最为深刻。<br />
<br />
进台湾馆祈福儿子李戡已是“接班人”<br />
<br />
从中国国家馆行至台湾馆，李敖心情似乎更加感慨。他笑着说：“原来我以为我来了大陆，就忘记了台湾，现在走进台湾馆，我发现原来我没有忘记，一切又都想起来了。”<br />
<br />
言语虽然幽默，李敖的举止却庄重起来。在台湾馆的祈福天灯许愿处，李敖从幸福美满、前程似锦、阖家平安等15组祈福语中，两度选择“两岸和平”，并表示“爱台湾，就是要承认台湾是中国的一部分”，令在场人士为之动容。<br />
<br />
面对来自海峡两岸的媒体记者，李敖说自己是最有资格说台湾的人：“我在台湾居住了61年，期间只有12天因来大陆演讲而离开。在这段时间里，我亲眼看到了台湾的成长。其实，现在的我很忧虑，忧虑台湾没有视野。”台湾馆馆长叶明水则回应道：“台湾馆来到上海世博会的目的，就是为了拉近两岸民众之间的感情。”<br />
<br />
与2005年“李敖神州文化之旅”相比，李敖这次的大陆之行显得亲情浓浓，特别是儿子李戡，几乎与李敖寸步不离。虽然昨天人多拥挤，场面一度十分混乱，李戡却始终伴其左右，不时搀扶李敖，李敖也经常顾盼四周，把儿子揽在身边，父子深情自然流露。<br />
<br />
当有记者问到李戡的发展时，李敖不无骄傲地说：“他已是我的接班人。”</div>

							
							
							
							
