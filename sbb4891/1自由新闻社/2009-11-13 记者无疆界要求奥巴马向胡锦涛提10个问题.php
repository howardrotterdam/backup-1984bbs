																											<div id="postmessage_325001" class="t_msgfont">记者无疆界要求奥巴马向胡锦涛提10个问题<br />
<br />
作者&nbsp;&nbsp;马丁<br />
<br />
发表日期 12/11/2009 更新日期 12/11/2009&nbsp;&nbsp;22:47 TU<br />
<br />
美国总统奥巴马12日启程，开始他为期9天的亚洲之行。按照白宫公布的奥巴马行程安排，他先到日本访问，然后前往新加坡参加在那里举行的亚太领袖峰会，最后于本周末（11月15日）抵达上海，然后前往北京；奥巴马将在中国停留3天，届时将会晤中国国家主席胡锦涛。<br />
<br />
就在奥巴马动身之时，总部设在巴黎的记者无疆界组织（RSF）在其网站上发表一份公开信，要求奥巴马总统与中国国家主席胡锦涛会晤时，向胡锦涛提10个问题。<br />
<br />
这10个问题主要涉及中国日益恶化的的人权状况和言论及新闻自由状况，包括关押敢说话的大批新闻记者、博客作者、知识分子、艺术家和异议人士，限制外国记者进入西藏和新疆地区采访，对网路实行封锁，等等。记者无疆界组织希望奥巴马能向中国胡锦涛提出这类问题。<br />
<br />
记者无疆界组织的10个问题如下：<br />
<br />
――中国当局为什么封锁推特（Twitter）和Facebook等美国网站？<br />
<br />
――美国政府资助的自由亚洲电台和美国之音的中文、藏文和维吾尔文广播为什么一贯遭到中国政府干扰？<br />
<br />
――上百名在被中国被囚禁的记者、博客作者和知识分子，包括刘晓波、胡佳、师涛和齐崇怀，能不能得到中国国家主席的特赦？这些人只不过是和平表达了自己的观点而已。<br />
<br />
――为什么外国记者，尤其美国记者，不能自由到西藏采访？<br />
<br />
――为什么西藏电影制作人当知项欠（Dhondup Wangchen）被以“颠覆国家政权”罪判刑，而他不过是制作了一部反应藏人诉求的纪录片而已？<br />
<br />
――为什么国际新闻社，尤其美国的新闻社，不能直接向中国媒体出售新闻稿？<br />
<br />
――为什么中共宣传部有权利对国际新闻进行经常性的审查？<br />
<br />
――为什么中国的警察和安全部队阻止记者到中国与朝鲜的边境地区调查和采访？<br />
<br />
――为什么新疆的居民近4个月来的通信（包括互联网和电话）被切断并受到监视？<br />
<br />
――为什么记者调查官员腐败，总是受到公安和宣传部门的压力和阻挠？人们注意到，以揭露贪官而著名的《财经》杂志，编辑部大部分人员刚刚因此而被迫集体辞职。<br />
<br />
记者无疆界组织在公开信中说：“如果奥巴马向中国领导人胡锦涛提这10个问题，得到胡锦涛的回应和保证，那么中国的言论自由和新闻自由就会有进步。”但是我们可以预料，即使奥巴马向胡锦涛提这10个问题，胡锦涛也不会正面回应，至少不会所有问题都正面回应。因为这些涉及人权和言论自由的问题，正触及中国政治制度的核心；如果胡锦涛答应改变这些情况，那么，共产党在中国的统治地位就会动摇。因为这个制度正是靠政治上的专制和限制人民的自由来维持的。<br />
<br />
有报道说，美国总统奥巴马15日到达上海后，希望能与上海的年轻人和大学生对话，有可能的话，更希望能会晤中国异议人士。奥巴马的这些希望和要求，已经惊动了上海市长和党委高层，他们已经在大学里挑选青年大学生，并将这些学生集中起来培训，接受党的教育。与此同时，当局为了防止上访人士借此机会呼吁美国关注中国人权问题，已提前软禁或“转移”活跃的维权人士。<br />
<br />
中央社12日发自上海的报道说，奥巴马15日抵达上海，16日将与上海市长韩正会晤，紧接着可能安排在上海市政府内和青年们对话。中央社引述上海交通大学等消息人士报道说，准备安排与奥巴马对话的大学生，都是从上海各高校严格挑选出来的，大约有50人，他们已经被提前集中到一个特定地点培训，规定对话中不能逾越的话题和范围，这些大学生的日常活动也部分受到限制。当局不希望在与奥巴马对话的大学生中间，出现与官方调子不一致的人。据报道，上海大学生与奥巴马对话，时间大约2小时。<br />
<br />
美国领导人访华期间会见大陆民间人士似乎已成惯例，奥巴马此行也不例外。报导说，美国方面已经准备了多种方案，首选名单是刘晓波的妻子刘霞和胡佳的妻子曾金燕。由於刘晓波和胡佳都曾获得诺贝尔和平奖提名，身为今年和平奖得主的奥巴马也希望与他们见面，但两人仍在关押和服刑，见到奥巴马的机会微乎其微。会见名单上也包括了“零八宪章”的签署人，但报道称这些人在奥巴马访华期间行动不一定自由，因此见面的机会也不大。至于记者无疆界组织提出的10个问题，在奥巴马与中国青年的对话中，被谈及的可能性就更小了。</div>

							
							
							
							
