													<h2>郭德纲书籍音像制品遭下架处理</h2>
						
						
																											<div id="postmessage_773538" class="t_msgfont">本报讯（记者孙静实习记者刘靖宜 李晓雨）今天记者从京城各大书店了解到，郭德纲的书籍和音像制品已全部下架。<br />
&nbsp; &nbsp; 郭德纲弟子李鹤彪殴打登门采访的北京电视台摄像记者后，郭德纲近日连续发表激烈言论。该事件已引起社会广泛关注。<br />
&nbsp; &nbsp; 今天上午，记者先后联系了中关村图书大厦、王府井图书大厦和北京图书大厦，相关工作人员表示，有关郭德纲的《逗你玩》、《话说北京》等书籍在昨天已全部下架，郭德纲的对口相声等音像光盘也遭封存。<br />
&nbsp; &nbsp; 北京图书大厦音像部门的工作人员说，昨天收到相关通知已将郭德纲的音像制品下架，暂时封存在仓库。<br />
&nbsp; &nbsp; 今天上午，记者拨打王府井图书大厦的电话购书和团购热线，销售人员表示，关于郭德纲的图书和音像制品已全部下架，在这些书店目前已经买不到“老郭”的书了。<br />
&nbsp; &nbsp; 而具体什么时候再上架恢复销售，店内人员表示，这要看书店的安排，时间暂还不能确定。<br />
&nbsp; &nbsp; 记者从亚运村图书大厦了解到，关于郭德纲的音像制品及图书已也作下架处理，并将及时更新电脑查询信息。<br />
记者目击<br />
一本书“漏网”被发现<br />
&nbsp; &nbsp; 中关村图书大厦内有关郭德纲、德云社的图书、音像制品陆续下架。今天上午，因为读者翻看后插错书架，一本“幸存”的郭德纲图书被发现后，最终下架。<br />
&nbsp; &nbsp; 该图书大厦音像区内，已经找不到郭德纲、德云社有关音像制品的踪影。工作人员介绍，上述商像制品约10种已全部下架。<br />
&nbsp; &nbsp; 图书区工作人员同样表示，有4种共8本与郭德纲、德云社有关的图书下架。随后记者在戏剧文学区内，发现一本郭德纲《话说北京》。<br />
&nbsp; &nbsp; 工作人员发现后，立即将其下架放入退货筐内。并解释，可能是读者翻阅该书后，将其插错了地方，才使得其在昨天的清理中“漏网”。 <br />
<br />
