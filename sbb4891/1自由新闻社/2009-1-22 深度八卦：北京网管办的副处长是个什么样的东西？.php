													<h2>网管之王陈华：管理网络，涛哥算老几？（转）</h2>
						
						
																											<div id="postmessage_25796" class="t_msgfont">2009年1月23日<br />
<br />
新华社记者张涛：网@管@办的陈华是个什么样的共产党员？ <br />
<br />
也许年纪太大，也许忙于给新华社写稿，我一般是不上网的。只知道网络媒体管得比较松，后来也看过一些网络上的文章，发现尺度放的确实比较开，如果不是年岁大了，总是觉得电脑这种东西很别扭，我也许会转到网络部工作。虽然没有到网络部，但对网络一直怀着很大的期待，当然网络很杂，也很开放，鱼龙混杂，应该有相对完善的管理，也说得过去。<br />
在国外新华分社工作了九个月回到北京，感觉到不上网真的不行了，因为在海外看到的纸媒，也是我过去九个月主要的新闻源的报纸，在国内没有了，要上网才能找到。女儿听说我要上网，很热心地帮忙，更新了我的电脑，连接了最新的宽带，为了进入海外网站，还给我装了代理，说是可以突破网络封锁。虽然有点慢，但毕竟又看到了国内看不到的报纸。<br />
<br />
晚上吃饭，儿女和我谈起了国内对网络的管理，她提到上个月在饭桌上认识的一位北京网@管@办的叫陈华的领导，女儿说起这个人眼睛就亮了。我一开始没有兴趣，结果才听了几句，就放下筷子让女儿讲下去，女儿反而有些迟疑了。最后我答应她，不写新华社内参，她才继续讲。<br />
<br />
女儿说这位四十多岁的陈华大概是北新办的网络@管理官员，他正在追求女儿的一位好友，女儿的好友和女儿同岁，今年23岁。陈华不但有老婆，而且本来就有几位不清不楚的女朋友，还在办公室里和新大学生调情，这些都是他自己在酒桌上吹嘘的。女儿的朋友之所以没有答应他，当然不是因为这些，反正现在的年轻人都是玩玩，只要你有钱，就无所谓。<br />
<br />
听到这里，我有些好奇，我问，一个管理网络的小官员，怎么会有钱？女儿表情很夸张地说，这你就不知道了，女儿的女朋友告诉她，为了追求年轻女子，这位四十的陈华不但请女孩到最昂贵的餐厅吃饭，而且还一掷千金。陈华还在吃饭中透露自己不但有钱，而且权力非常大。女儿的朋友当然半信半疑，然而，陈华开着豪华轿车可没有错，陈华在吃饭时说，我的车比北新办和所有网络管理领导的车都好，你说，我靠那工作，能买这样的车？<br />
<br />
女儿透露，这位陈华已经不是一次得意洋洋地宣称自己生活富裕，再养活几个女孩也没有问题，但他要养就要养自己真正喜欢的。他还在一次聚会中说，现在中国的经济不好，但我们是公务员，旱涝保丰，而且，越是经济不好，来自北京以外的女孩要价就越便宜，他还说，这就是市场经济，所以我支持市场经济，但我不支持资本主义那一套，否则，我这个网络管理者就没有工作了。<br />
<br />
就是这个陈华，在追求女儿的朋友失败时气急败坏，竟然说，他在北京各个银行都有存折，老婆已经搞定了，他还有两套房子，光租金就够她生活的。女儿的朋友不服气，说你一个网络管理人员，除非你换工作，你靠什么养活我。陈华说，你都不知道我多有权力，我管理的网站，他们要想发展，就得我点头，否则，他们就死了，你知道我管理的都是哪些大网站吧。他还说，共产党也得吃饭，社会主义养活不了我们了，但我们还得靠社会主义发财，怎么靠，就是要在网络上找一些人的毛病，让他们知道我的厉害。他说，他就把自己管理的网站管死，谁不和我搞好关系，我就要他们要死不得，要活不能。他还说，要让这些网民知道他们那点言论自由是谁给的DD就是我陈华给的！<br />
<br />
我听后感觉不真实，我不相信一位共产党员会对女儿那样年纪的女孩子说这种话，女儿一听就说，你不信我的朋友，因为你不知道我朋友是干什么的，以及他们的关系。女儿说，她的那位23岁的朋友在中关村干过一年，因为不景气离开了，现在就是想通过陈华到大网站新浪这样的地方工作，因为是朋友介绍的，陈华本来答应了，但等到见面看到她年轻的美貌，就改变了主意，老是约会她，最后明确提出要交朋友，想先玩弄23岁女孩几年，还说，网站那工作很可怜，你不用去工作，我让他们发工资给你就行了，还说，你可以在家里工作，也可以，我管很多网站的。<br />
<br />
女儿说，你可以不相信我的朋友，因为她没有答应陈华，也没有去新浪，所以她可能带感情说话。但你不能不相信我，她说，她和朋友一起与陈华吃过两次饭，这位陈华实在牛，他说起自己管理的网络，简直像说起自己养的一条狗。他说，什么上面命令，管理网络，胡@锦@涛算老几？温@家@宝更不是个东西，管理网络就是由我们说了算。他说，对于一些网站，他就是总书记，他就是总理，网民算个屁，胡@锦@涛和温@家@宝也只不过是一个屁，因为他们根本不上网，别听上面宣传的。<br />
<br />
在喝多了酒后，他还说，以前管严一点是害怕出事，现在管严就是不愿意看到他们太嚣张，特别是对公务员攻击的文章，他看一个要求删一个。他还说，干到四十多岁，他算是明白了，只要不犯政治错误，就算吃喝嫖赌都不是问题，他对自己现在的领导非常不满，说如果现在再来一场文化大革命，他第一个带领群众冲上去把新闻办和宣传部砸乱，他说那些领导都是右派分子，都对网络管理不够严。他说，总有一天他会成为网络管理之王的。可是在另外一个场合，他竟然对朋友说，如果你搞网站我会支持的，而且只有我可以支持你，我会把与你竞争的网站都管得一动不动，你就可以发展。那一次他接收了人家的一个红包，说是圣诞节给孩子买礼物。<br />
<br />
我听完女儿所言，心情久久不能平静，从事新闻工作这么久，我知道我们存在的一些问题，有些问题可以改，有些积重难返，然而，我还是第一次从自己女儿的口中听到一位现在北京网@络管@理公务员说出如此的话。女儿还告诉我陈华其他一些事，我却没有心思听了。我想，既然答应了女儿我不能写内参，但我并没有答应他使用假名写一些文章发到海外，陈华好像管不了海外的网站。<br />
<br />
我想，作为一名有三十二年党龄的新闻工作者，我也许写过不少昧良心的新闻，但我没有见过如此无耻的新闻管理人员。女儿说的还有很多具体的事，有些实在太具体，我需要进一步证实。现在我以张涛的笔名把这个人揭露出来，他的主要犯罪事实是：<br />
<br />
一，利用工作之便，受贿，目前财产中有大量不明来历，除了他自己的三个银行帐号和老婆的帐号，他的亲戚中还有一个帐号。我恳求北京司法机关立即介入，免得他转移资产，适当的时候，我将把联系好的人组织起来，到法庭作证。现在请有陈华的主管部门和公安等司法机关立即到银行和房地产登记处查出他的登记信息。<br />
<br />
二，此人在工作中不顾法律和规定，实行私人报复，联合与人利用网络和网络管理赚钱；身为国家公务员，常常攻击胡@锦@涛和温@家@宝，说他们是右派，迟早要下台，而他就是宣传部和国新办的未来领导，到时，他要把现任领导都关到秦城监狱。此人不适合在网络管理部门工作，请领导们调查并作出决定。<br />
<br />
三，此人玩弄女性，利用工作之便，约会各大网站女子，以共产党的名义搞她们。严重败坏了党的清白。<br />
<br />
还有几条，目前正在收集证据。为了给他一个投案自首的机会，我们先不公布他的身份证号码，但也为了提起有关部门注意，避免重复名字造成伤害，我们现在公布他的手机：陈华，手机：（1392407无敌网管）。实际上，他还有另一个手机号是：1391074（无敌网管），他的电子邮箱是：<a href="mailto:chenhua@mail.21dnn.com">chenhua@mail.21dnn.com</a>、<a href="mailto:huachen@beijing.gov.cn">huachen@beijing.gov.cn</a>。<br />
<br />
我一边呼吁政府不能对这种坏分子听之任之，另外一方面，我在这里以一位老新闻工作者的身份向我不熟悉的网民发出呼吁，请全国所有有条件的网民，展开对这位败类的人肉搜索。搜索内容包括银行帐号和资金来往，信用卡使用情况，所有宾馆旅店开房情况，他的房产和小车的不明来源部分，以及他利用网络管牟利和玩弄女性的种种劣迹。请大家把搜集的资料暂时不要在网络曝光，汇总给我，然后我将在适当时候给新华社写内参，如果还有领导包庇，我将以一位老党员和老新闻工作者的革命意志发誓，我会把包庇他的领导也纠出来，我将给胡温直接上书，揭露他们的腐败。<br />
<br />
为了网络的清洁，请大家分头行动吧，这种败类还在网@管@处干一天，中华民族的不幸就会多一天。<br />
<br />
新华社老记者、老党员：张涛于北京新华社</div>

							
							
							
							
