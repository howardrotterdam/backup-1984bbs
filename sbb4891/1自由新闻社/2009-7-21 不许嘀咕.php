													<h2>关于在全国范围内整治微型博客专项行动的通知(转)</h2>
						
						
																											<div id="postmessage_173871" class="t_msgfont">注意最后面o&nbsp;&nbsp;~~~~~<br />
<br />
<br />
<br />
<br />
关于在全国范围内整治微型博客专项行动的通知<br />
<br />
各省、市、自治区人民政府，各有关厅，各有关院，各有关部：<br />
<br />
为贯彻落实全国净化文化环境工作会议精神和五毛办、怎么办《关于进一步净化社会文化环境促进网民健康成长的若干意见》（咋办发[2009]64号）文件精神，进一步加强对全国互联网站的规范化管理，推动全国互联网站文明办网，健康发展，决定在全国开展整治微型博客集中整治活动。现将有关事项通知如下：<br />
<br />
Twitter Oops<br />
一、指导思想<br />
<br />
&nbsp; &nbsp; 通过开展微型博客集中整治活动，进一步净化网络空间，营造文明、和谐的绿色网络环境，提升我国互联网办网水平和质量，把互联网真正办成宣传科学理论、传播先进文化、塑造美好心灵、弘扬社会正气的阵地，为广大网民提供更好的信息服务，推动我国互联网健康有序发展。<br />
<br />
二、集中整治活动范围<br />
<br />
&nbsp; &nbsp; 墙内所有经营性互联网站和非经营性互联网站，包括党政机关、群众团体和企事业单位以及个人主办的微型博客（内部局域网除外）。<br />
<br />
三、集中整治活动时间及工作安排<br />
<br />
&nbsp; &nbsp; （一）活动的时间安排<br />
&nbsp; &nbsp; 整个集中整治活动从2009年5月底至10月底共五个月的时间，分自查、群众举报及抽查、整改总结三个阶段进行。<br />
<br />
&nbsp; &nbsp; （二）各阶段工作安排<br />
&nbsp; &nbsp; 第一阶段：自查阶段（2009年5月25日至10月23日）<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;按照《互联网信息服务管理办法》、《互联网IP地址备案管理办法》、《非经营性互联网信息服务备案管理办法》等国家有关互联网管理的法律、法规和《关于加强互联网站备案管理的工作方案》的要求，墙内所有经营性互联网站和非经营性互联网站已通过具有互联网接入服务资格的服务商备案的，要将备案资料上报县信息产业局；凡未在任何机构进行备案的，要填写网站备案信息表，通过为其制作网站的代理商进行备案，并将备案资料上报县信息产业局；因代理商联系不上或其它原因而不能进行备案的，自己看着办。所有网站都要对照以下方面进行自查：<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;1. 是否刊载国家法律、行政法规禁止的内容以及不健康文字和图片；<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;2. 是否链接不健康网站，提供不健康内容搜索，发送不健康短(彩)信，开设不健康声讯服务；<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;3. 是否登载不健康广告，发布虚假信息；<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;4. 是否在微型博客等栏目中发表、转载违法、虚假、庸俗、格调低下的言论、图片、音视频信息。<br />
<br />
&nbsp; &nbsp; 第二阶段：群众举报及抽查阶段（2009年10月24日）<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;公众对发现的存在以上问题的网站，可通过举报电话（对不起，您所查看的号码余额已不足，无法拨通）或写信至贵国信息产业局（地址： <a href="mailto:xinxichanyeju@guiguo.cn">xinxichanyeju@guiguo.cn</a> ）进行举报。贵国信息产业局将协同有关部门对部分网站进行抽查。<br />
<br />
&nbsp; &nbsp; 第三阶段：整改总结阶段（2009年10月25日）<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;对群众举报和抽查出的未进行备案和存在问题的网站，按照国家有关法律、法规，通知其互联网接入服务商对其进行关闭，触犯国家法律法规的，对相关责任人依法进行处罚。对这次网络环境集中整治活动进行总结，报天朝相关委、天朝相关政府有关领导。<br />
<br />
四、工作要求<br />
<br />
&nbsp; &nbsp; 各省、市、自治区人民政府，各有关厅，各有关院，各有关部要在积极落实集中整治活动各项工作的基础上，通过这次活动，使网络环境得到净化，网上风气得到全面好转，工作要求如下：<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 1. 开展集中整治活动是加强网络环境治理，净化社会文化环境，保护网民的一项基础工作，各省、市、自治区人民政府，各有关厅，各有关院，各有关部要高度重视，加强领导，靠前指挥，周密部署，切实加大清理整治力度，切实落实各项工作责任。<br />
&nbsp; &nbsp;&nbsp; &nbsp; 2. 按照“谁主管、谁负责，谁运营、谁负责”的原则，各有关政府负责所属辖区内的集中整治工作，各有关部门负责本系统内的集中整治工作, 实行责任分工制和责任追究制。<br />
