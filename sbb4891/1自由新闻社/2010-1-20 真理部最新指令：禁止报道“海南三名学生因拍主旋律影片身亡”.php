													<h2>剧组每天给校长2500元 学生当演员1死2失踪</h2>
						
						
																											<div id="postmessage_441787" class="t_msgfont">来源:中国青年报　更新时间:2010-1-20<br />
<br />
在海上搜寻了48小时后，失踪的两名学生仍然未能找到。一名参与搜救的民警告诉记者，他们的生还机会几乎为零。 <br />
<br />
　　而在此前，1月15日下午4点左右，前后共有10人被巨浪卷入海底。除了两个失踪的学生，还有1人被救出送往医院后不治身亡，侥幸生还的7人伤势或轻或重，目前仍躺在海南省文昌市人民医院的病房里。 <br />
<br />
　　这些遇难学生来自文昌市一所民办中学孔子中学，他们没有想到，当初学校动员会上所说的一次课外活动，竟是因为：学生做群众演员参与30集电视连续剧《解放海南岛》的拍摄，剧组每天会给校长2500元；他们更没有想到，学校所说的锻炼机会，竟然让自己的3名同学因此丧生。 <br />
<br />
　　高中生当上了群众演员&nbsp;&nbsp;<br />
<br />
　　1月14日，星期四。一大早，孔子中学高一、高二年级共8个班的学生刚用完早餐，就接到班主任的通知：吃完饭后到操场集合，学校有事宣布。 <br />
<br />
　　在操场上，校长和副校长发表了讲话，大意是：这几天，学校将在你们中间挑选100多名群众演员，参加《解放海南岛》长篇电视连续剧的拍摄。 <br />
<br />
　　校领导们称：这部电视剧选中了孔子中学，整个海南省也只有孔子中学的学生可以作为群众演员，说明学校获得了肯定，也给同学们提供了一次锻炼机会。 <br />
<br />
　　在场的大部分学生表现得很兴奋。“终于有机会参演电视剧了。”高二(3)班的付冬冬说，虽然他不知道具体要演些什么、在哪里演，但他还是十分相信校领导和班主任的话。他自己把参演电视剧的事当作一次课外活动，没有告诉自己的父母。 <br />
<br />
　　有些学生则小声嘀咕：马上要期末考试了，学习很紧张，学校怎么在这个时候让自己当群众演员，这不是要影响学习吗？ <br />
<br />
　　一位班主任在知道了学校的决定后，心里有些打鼓：先不说影响学生的学习，这么多的学生参加演出，安全上怎么保证？学校有没有和剧组签订协议？有没有为参演的学生买保险？ <br />
<br />
　　一名落水被救起的学生告诉中国青年报记者，1月14日开完动员会后，剧组从列队的学生中挑选了100多名同学，除在身高上有所要求外，剧组没有别的要求，不管会不会游泳，也不管身体是否舒适。 <br />
<br />
　　选定了群众演员后，学生们被大巴车拉到了文昌市的白金海岸，开始了第一天的拍摄。当天虽然没出现什么问题，但还是有学生向班主任反映：好累啊。更有个别学生说，道具很重，扛了一天，肩膀磨得很痛。 <br />
<br />
　　就在这一天，按照当地警方调查的结果，校长刘德斌从剧组那里收到了2500元现金，这笔钱没有交给学校的财务入账。 <br />
<br />
　　在此前，刘德斌已经通过海南一家协会，和负责《解放海南岛》具体拍摄的北京一家传媒公司私下达成了口头协议：学校组织学生当群众演员，剧组偿付一定的费用。 <br />
<br />
　　一名班主任告诉记者，他听说校长和剧组的负责人是湖北老乡。 <br />
<br />
　　一个大浪打过来，我被海水卷走了 <br />
<br />
　　第二天，100多名学生继续充任群众演员，只是这次改变了拍摄场地：由文昌白金海岸转移到了铜鼓岭大澳湾。 <br />
<br />
　　文昌市市民韩先生告诉中国青年报记者，大澳湾常年风大浪急，海底暗流涌动，本地只有一些非常熟悉海情的渔民才会在大澳湾下海。 <br />
<br />
　　然而，对剧组来说，大澳湾却更适合拍摄解放军从海上登陆的画面。虽然通往大澳湾的路有一段正在翻修，崎岖不平，剧组还是在1月15日把学生拉到了这里。 <br />
<br />
　　躺在病床上的付冬冬回忆，当时，大澳湾的风特别大，浪特别高，海水特别凉，同学们被组织起来排成数个队列，走到齐腰深的海水中。剧组的摄影机架在沙滩上，同学们要迎着镜头从齐腰深的海中冲向岸边。 <br />
<br />
　　下午3点半左右，剧组和学生正集中精力进行拍摄，穿着解放军军服的100多名学生正向海岸移动。 <br />
<br />
　　没有一个学生察觉，这时的海底已经发生了很大变化。在十几名同学的前方，海底已形成了一个很深的沙坑，由此产生的暗流正在海底狰狞旋转。 <br />
<br />
　　同学们靠近沙坑的时候，身后一个大浪打来，一些人开始东倒西歪。紧接着再一个大浪打来，十几名同学被卷入海中。 <br />
<br />
　　此时，剧组和所有参演的学生慌了神，大家迅速开始自救。虽然有几名同学被当场救出，但仍有一些学生不见了踪影。 <br />
<br />
　　文昌市公安局很快接到了报警电话，这一消息也立即被报告给正在海口开会的文昌市市委书记裴成敏。 <br />
<br />
　　裴成敏当即要求举全市之力展开救援，并赶到了大澳湾。最终仍有一人经抢救无效死亡，两名学生失踪。 <br />
<br />
　　1月16日上午，记者在大澳湾现场看到，直升机在海面上仍在盘旋搜寻失踪学生，边防武警和公安民警则在大澳湾两岸寻找。&nbsp;&nbsp;<br />
<br />
　　一名警方负责人称，地方已组织了潜水员在海底搜寻，大量渔船也参与进来，将搜寻范围扩展至周边海域。 <br />
<br />
　　失踪学生的几个家长随后被带到了大澳湾，他们由当地政府的工作人员搀扶着，边走边哭着说：“怎么会这样啊？” <br />
<br />
　　记者了解到，一名遇难的学生和两名失踪的学生是高二年级的，他们均是文昌本地人。 <br />
<br />
　　哀伤气氛笼罩了孔子中学 <br />
<br />
　　事故发生后，孔子中学校长刘德斌被警方控制，学校的大门也紧闭着，外人不经允许，一律不得入内。 <br />
<br />
　　刘德斌的手机仍保持着开通，他在电话中告诉记者，自己在文昌县城的一个派出所里，能否接受采访要向警方请示。警方拒绝了记者的采访请求。 <br />
<br />
　　1月16日，记者在孔子中学的门口看到，一些学生表情沉痛地在学校内徘徊。还有几名女生找来了自己的老师，由老师出面在保安室签字登记后，才被放了出来。 <br />
<br />
　　一名女生眼里噙着泪水告诉记者，她是高二年级的学生，要去医院探望被救出来的同学。 <br />
<br />
　　记者拨通了孔子中学教导主任的电话，他表示，目前市政府有关人员正在学校调查，没有时间接受采访。 <br />
<br />
　　1月16日下午，文昌市有关部门负责人告诉记者，目前还不允许向记者透露此次事故的具体情况。 <br />
<br />
　　<strong>记者发现，1月15日晚8点，海南本地一个有关文昌的论坛出现了关于此次事故的帖子，第二天再打开这个论坛时，帖子已不见了踪影。 </strong><br />
<br />
　　事故当天，也有一些海南本地媒体记者赶到文昌进行采访，然而到截稿时为止，尚未见到有媒体对此事进行过相关报道。 记者 任明超</div>

							
							
							
							
