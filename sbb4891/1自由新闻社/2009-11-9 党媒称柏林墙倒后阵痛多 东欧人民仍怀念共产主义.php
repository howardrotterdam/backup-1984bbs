													<h2>党媒称柏林墙倒后阵痛多 东欧人民仍怀念共产主义</h2>
						
						
																											<div id="postmessage_319633" class="t_msgfont">柏林墙倒塌20年：新东欧仍在经历转轨之痛<br />
2009年11月09日10:38 　 新华网<br />
<br />
柏林墙倒塌20年后，东欧转轨国家在享受种种变化的同时，依然体会着转轨带来的种种“撕裂”之痛<br />
<br />
《国际先驱导报》记者胡小兵、刘向、张章、林惠芬、孙希有、杨柯、陈Z贝、杨梅菊发自柏林、华沙、布加勒斯特、布拉格、地拉那、北京 岁月悠悠，2009年11月6日，记者再次来到这里，完整的柏林墙已不复存在，只有缪伦大街旁的那段1.3公里长的柏林墙还矗立在那里，墙面的画被重新彩绘一新，一些游人在画前拍照纪念，一些老人在墙下徘徊……<br />
<br />
柏林墙倒塌20周年将至，德国人精心准备近一年时间：如今国会大厦已粉饰一新，电视报纸每天铺天盖地在回顾和总结20年来的德国统一变迁，勃兰登堡门前搭起了庆祝的舞台，欧盟各国首脑们将应邀在11月9日会聚柏林，参加这场庆祝活动。德国总理默克尔说：“这将是我最快乐的一天”。活动高潮将把1000块宽约1.3米、厚0.2米、高2.5米的柏林墙象征性墙块像多米诺骨牌一样推倒。<br />
<br />
不过，横隔在人们心中的墙，或许无法那么轻易地轰然倒塌。<br />
<br />
是融入，还是被吞噬<br />
<br />
与20年前相比，今天的柏林乃至东德不可同日而语。1991年，原东德的人均国内生产总值为西德的43%，如今这一比例是71%。同期内，东德的人均可支配收入与西德之比从60%升至80%。从宽带光缆到崭新的公路，东德的公共基础设施甚至比西德还要好。<br />
<br />
有这样一个例子，前东德生产的“卫星牌”轿车曾经满街都是，在剧变之后，一下子就被西德的高档车淘汰掉了。一位最近访德归来的中国专家记述在柏林帝国教堂附近看到一辆“卫星”，而“众人纷纷拍照，仿佛看到了大熊猫”。<br />
<br />
不过，一种心理失落感也相伴而生，特别是那些五六十岁、在东德时期成长起来的德国人。<br />
<br />
60多岁的东柏林人霍斯特・埃克特博士就有这样的感触：“我们的生活曾经不同，我们的习惯不同，我们不打算放弃这些习惯。”<br />
<br />
柏林墙被推倒后，东德人很快就可以买好车、住上好点的房子，但失业问题也随之出现。根据最新的数据统计，德国东部的失业率是西部的两倍。埃克特博士说，东德人很在乎工作，失业对东德人就是一场灾难，西德人对此感到惊讶，因为如果他们失业，可以先去国外玩一整年，然后回来领救济。<br />
<br />
有一丝怀念，亦有几分淡忘<br />
<br />
在采访中，记者发现，几乎每一个接受采访的东欧人都不否认剧变及转轨之后，经济取得了迅速的发展，但记者也发现，并不是每一个都满意自己现在的生活水平。<br />
<br />
巴希雅才21岁，看上去很成熟，谈起国家的现状，她一脸的冷漠，看着远方，好像在和记者说着一个和她毫不相关的话题。<br />
<br />
“你可以想像100美元的月薪生存有多么困难吗？”她告诉记者，由于经济危机的到来，罗马尼亚的经济更加雪上加霜。<br />
<br />
今天，罗马尼亚人早已实现了20年前在街头高喊的愿望：“反对独裁”“言论自由”“出行自由”“民主选举”“多党制”……然而，在政坛，朝野两派相互攻讦，执政联盟联而不盟，政府重组接二连三；在街头，示威抗议频频不断，人们要求按时发放工资、确保就业、提高退休金……<br />
<br />
这种情况下，怀旧甚至成为一种时尚。如果说罗马尼亚人在1989年后有相当一段时间曾经刻意去丢弃、毁坏“齐奥塞斯库时代”所有印记的话，如今，人们已经开始平静、客观地去谈论那个时代和齐奥塞斯库这个人物，甚至收藏社会主义时期的物品。<br />
<br />
根据罗马尼亚著名的民调机构库尔斯的一项调查，27％的罗马尼亚人认为1989年前生活得更好，比持相反意见的人多出4个百分点；在回答“今日罗马尼亚印象时”，竟然有66％的人以“糟糕”作答，只有12％的受访者给予正面评价；而在谈到“社会主义时期最令人留恋的方面”时，43％的人认为当年生活和工作的基本保障是大家最美好的记忆！<br />
<br />
不过，怀念过去并不等于人们想要回到过去。中国社科院东欧问题专家朱晓中就认为，“人们留恋的是过去的便利、稳定，而非一个社会制度。”国务院发展研究中心专家苗华寿也指出，“如果真的要他们回去，那愿意的人其实很少。”<br />
<br />
事实上，今天很多东欧的年轻人并不了解那段历史。19岁的保加利亚女孩艾维特认为，今天的保加利亚年轻人一般对20年前的事情没有什么兴趣。27岁的捷克男子博智洋则告诉记者，捷克中小学的历史书里，只有古代史和到二战之前的近代史，二战之后，捷克发生了什么，学校根本就没有教授给学生。<br />
<br />
热闹的舞台，冷漠的选民<br />
<br />
11月22日，罗马尼亚将举行5年一度的总统选举。这是剧变后该国第6次总统选举。不过，一位曾经是工程师的出租车司机却对记者说：“我不会去投票，选举纯粹是政治家愚弄百姓的游戏。我曾经满腔热情去参加投票，希望新政权能改变我的生活，可是，现在我却在开出租车。”<br />
<br />
这位司机的选择并非孤立现象。尽管各候选人正在进行如火如荼地竞选活动，但罗马尼亚选民对政治的热情、对候选人的期待今非昔比。民调显示，12位候选人在第一轮投票中将无一获得绝对多数胜出。<br />
<br />
中国政法大学教授金雁认为，目前新欧洲国家一个突出的特点就是“政治冷漠化”。<br />
<br />
不可否认，剧变后，东欧人的确曾一度非常热情地拥抱多党制――这一在西方被标榜为最民主的执政方式。<br />
<br />
以捷克为例，20年来，在捷克这个只有1000万人口的国家，最多时一下子冒出来150多个政党：喜欢喝啤酒的人成立了啤酒党，关心生态的人创建了绿党，维护地方利益的人组建起自治同盟，无政府主义者为参与国家事务也建立了共和党，滋事的年轻人成立光头党……<br />
<br />
但在热情冷却下来之后，人们却发现，问题并没有得到有效的解决。<br />
<br />
由于政党利益的纷争，捷克政府难以出台有利的经济和政治改革的政策。今年5月，捷克众议院通过了政府的刺激经济的一揽子计划。但总统克劳斯却在7月份否决了这一计划。克劳斯的否决并非毫无道理，但其主要目的并非强化应对经济危机的手段，而是显示总统的权力和威望。<br />
<br />
于是乎，东欧改行多党制后出现了一种奇怪现象：虽然人们有了言论自由，但无人倾听他们的呼声。<br />
<br />
