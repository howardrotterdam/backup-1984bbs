																											<div id="postmessage_320025" class="t_msgfont">11月9日是柏林围墙倒下20周年，分裂40多年的东西德，在柏林围墙倒下11个月后正式统一，然而政治上的统一，却无法收窄东西两地的经济差距，一度令旧东德人民感到失望。但最新研究显示，德国东部近20年经济发展速度实际已远超大部分人的想象，甚至最快在10年后便可追上德国西部落后地区。<br />
<br />
　　香港《文汇报》报道，许多首次走访德东的旅客，往往对焕然一新的火车站或游泳池等建设感到惊讶。近年东部亦出现多处工业重镇，包括设有宝马及保时捷车厂的莱比锡、半导体工业重镇德累斯顿、光学工业大城耶拿，以及太阳能板工业龙头Q-Cells总部所在的比特费尔德－沃尔芬等。<br />
<br />
10年内料赶超西德落后区<br />
<br />
　　据德国经济研究所(DIW)最新研究显示，与其他条件类似的国家相比，德东在围墙倒后的成长速度整整快了1倍，人均国内生产总值(GDP)从最初只有德西的33%，增长到目前的70%。若按照这速度发展下去，到2020年，德东人均GDP便可升到德西8成，超越以农业为主的石勒苏益格―荷尔斯泰因州等德西落后地区。<br />
<br />
　　DIW总裁许特强调，德东的发展并非如外界想象般缓慢，同时德西亦有发展不均的问题。但他亦认为，由于东部缺少法兰克福、慕尼黑或汉堡等金融商业中心，德东的经济可能永远也无法追上德西平均水平。<br />
<br />
商界：西部为统一付代价<br />
<br />
　　事实上，德东的发展很大程度是归功于过去20年来，由西部提供的多达1.3万亿欧元(约15万亿港元)援助，相当于德国去年全国生产总值。据另一项调查显示，多达4成德国政商界领袖认为，东西德统一对西部产生负面影响，反映西部地区不满为拉近东西经济差距所付出的代价。<br />
<br />
　　德东经济发展亦并非毫无隐忧。分析指出，由于机遇较少、技术落后，东部中小型城镇每年都会流失大量年轻人口，若要确保德东未来发展稳定，必须提升当地教育水平，培养并留住人才。</div>

							
							
							
							
