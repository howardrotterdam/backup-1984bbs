新疆法律专家就7-5案件审判答记者问<br />
<br />
2009年10月12日19:34&nbsp;&nbsp;人民网 <br />
<br />
　　人民网乌鲁木齐10月12日电 (记者戴岚)10月12日，“ 7・5”事件三起重大犯罪案件审理结束后，新疆维吾尔自治区党校法学部主任、教授、新疆法学会理事陈奇彪、新疆师范大学法经学院法律系副教授茹克娅・霍加和乌鲁木齐市人民检察院侦查监督处处长李岩接受了记者的采访。<br />
<br />
　　记者：今天，“7・5”事件三起重大犯罪案件7名被告人接受了审判，各族群众对庭审过程和判决结果都非常关注，请问两位专家和检察官对此是怎么看的？<br />
<br />
　　陈奇彪：依法从严从重对“7・5”事件的犯罪分子进行审判，彰显了法律尊严，体现了公平正义，是众望所归，人心所向。一切有良知、有道德、有正义感的人都是期盼这一结果、拥护这一结果的。<br />
<br />
　　通过今天对三案7人的审理和判决，使我们更加清晰地看到 ，“7・5”事件是一起由境内外“三股势力”相互勾结，精心策划组织的打砸抢烧严重暴力犯罪事件，也是一起残无人性地滥杀无辜群众的暴力恐怖事件。其险恶用心是妄图破坏民族团结，危害社会稳定，达到其分裂国家的目的。法律的尊严不容践踏，任何一个法治国家都不会允许打砸抢烧严重暴力犯罪行为，任何犯罪行为都应当受到法律的惩处。<br />
<br />
　　李岩：“7・5”事件是新疆解放以来发生的一起性质最恶劣、影响最大、后果最严重的打砸抢烧严重暴力犯罪事件，严重破坏了乌鲁木齐的正常秩序和我区社会稳定。这起事件共造成197人死亡，1803人受伤，有380户店铺被砸被烧，169辆车辆被烧毁，1208辆车辆受损。暴力犯罪分子的恶劣行径不仅引起了各族群众的强烈愤慨，也受到国际社会的强烈谴责。<br />
<br />
　　茹克娅・霍加：从今天的审理、判决的三案7人可以看出，“7・5”事件中的不法之徒，犯罪行径之恶劣、手段之残忍、主观恶意之严重，与发生在世界各地的严重暴力犯罪活动有过之而无不及。例如阿不都克里木・阿不都瓦伊提故意杀人、放火案，被告人在短短两个多小时，在五个现场连续作案，杀死5人，是“7・5”事件中一人致无辜群众死亡人数最多的案件之一。再比如说艾尼・玉苏甫等4人故意杀人、放火、抢劫案，共造成10人死亡，2人受伤，财产损失137万元，是“7・5”事件已立案件中一案死亡人数最多、犯罪后果最严重的案件之一。尤其是艾尼・玉苏甫伙同其他暴徒拦截一辆面包车，将车内一家五口人4人打死、1人打成重伤，并把面包车烧毁。之后，阿卜杜拉・麦提托合提又伙同他人打砸、焚烧天山粮油店，造成店主张赵氏一家五口人死于火中，而这五名遇害者年龄最大的82岁，最小的才12岁。这伙暴徒惨绝人寰的行径真是令人发指，这也是世界上暴力犯罪所少见的。所以，对“7・5”事件的暴力犯罪分子施以严厉的刑罚，不仅彰显法律的尊严，更是顺应民心民意的。<br />
<br />
　　陈奇彪：我认为，对“7・5”事件严重暴力犯罪分子处以重刑完全符合中国法律，也完全符合国际惯例。因为任何一个法治国家都不会容忍残杀无辜者的行为而不从严处罚，国际公约也没有禁止对罪行极其严重的暴力犯罪处以严刑。特别是上个世纪九十年代以来，针对暴力恐怖活动日益增多的现实，世界各国都制定有相应法律严惩暴力恐怖犯罪。比如，1993年美国通过法律，对以爆炸或其他恐怖活动造成他人死亡的犯罪分子，将以谋杀罪处以极刑。1995年日本“奥姆真理教”邪教组织在东京地铁释放毒气案的犯罪分子均被处以死刑。<br />
<br />
　　今天，乌鲁木齐市中级人民法院依据《刑法》、《刑事诉讼法》对参与“7・5”事件的7名暴力犯罪分子一审开庭审理，并当庭宣判，从严追究其法律责任，体现了人民意志，体现了社会正义，体现了法律尊严。<br />
<br />
　　记者：“7・5”事件至今已过去三个月了，为什么到现在才开始开庭审理第一批案件？<br />
<br />
　　李岩：“7・5”事件参与人数之多、犯罪现场之大，案情之复杂，在新疆乃至全国都是罕见的。据我们了解掌握，截至9月底，乌鲁木齐市公安机关确认“7・5”事件共涉及15个街巷数百个现场，先后立案多起， “7・5”事件的案件侦办难度之大、工作量之多是可想而知的。为此，“7・5”事件发生后，公安部、自治区公安厅抽调全国、全疆数百名侦查、审讯、技术专家参与“7・5”事件的案件侦办，仅乌鲁木齐市公安机关直接投入警力1千余人，全疆公安机关间接投入警力近万人，分别成立了专案侦查、甄别审查和调查取证三个工作组。检察机关和法院系统也抽调数百名检察官、法官参与案件审理工作。<br />
<br />
　　党中央和自治区党委对“7・5”事件的案件侦办、审理工作极为重视，明确要求各级政法部门，不但要全力以赴加快“7・5”事件的案件侦办审理，而且要严格依法办案，既讲办案效率，更要讲办案质量，务必把每一起案件办成经得起法律检验、经得起人民检验、经得起历史检验的铁案。按照这一精神，我们司法机关对“7・5”事件的案件都持非常慎重的态度，对每一起案件、每一个办案环节、每一件犯罪证据都进行严格反复甄别、审查，确保案件在程序和实体上完全符合法律规定。<br />
<br />
　　陈奇彪：是的。在国际上，如此大规模、案情复杂、涉及面广的案件，由于社会关注度高，极为敏感，各国司法机关一般都在认定事实清楚、证据确凿、适用法律正确、量刑准确等各个方面持非常谨慎的态度，因此，这类案件的审理周期一般都比较长。例如1992年4月29日发生在美国洛杉矶的骚乱、2002年10月12日发生在印尼巴厘岛的特大爆炸案、2007年10月26日发生在法国巴黎长达近20天的骚乱，从侦查终结到提起公诉直到庭审判决，都历时数年。特别是发生在美国纽约的“911”恐怖袭击事件已经过去8年之久，至今还没有完全结案。所以，乌鲁木齐市司法机关对“7・5”事件犯罪分子在三个月后提起公诉、开庭审判，体现了司法机关严格谨慎的执法态度，也符合法律、符合此类案件审理工作的实际。<br />
<br />
　　记者：那么，我国的相关法律对此又是怎样规定的？<br />
<br />
　　茹克娅・霍加：根据我国《刑事诉讼法》规定，检察机关审查逮捕的期限是7天，批准逮捕后公安机关一般在两个月内侦查终结移送起诉，检察机关一般在一个月内审查起诉，并作出相应的处理决定，一审法院一般在一个月内审结。也就是说，一般的案件从犯罪嫌疑人被羁押起到一审判决，正常期限为4个月。那么根据刑诉法的规定，办理重大复杂的案件时间还可以延长，在特殊情况下，最长可达一年多时间。而“7・5”事件系列案件就是非常典型的重大、复杂、疑难案件，乌鲁木齐市司法机关仅用了3个多月时间把3案一审审结，应当讲效率还是比较高的。<br />
<br />
　　记者：“7・5”案件发生在乌鲁木齐市天山区，为什么由乌鲁木齐市中级人民法院审理案件而不是天山区人民法院呢？<br />
<br />
　　茹克娅・霍加：我国共有四级法院，《刑事诉讼法》对不同级别的法院管辖范围都作了相应规定。《刑事诉讼法》第二十条规定，中级人民法院管辖下列第一审刑事案件：(一)危害国家安全案件；(二)可能判处无期徒刑、死刑的普通刑事案件；(三)外国人犯罪的刑事案件。可以看出，中级法院管辖的三类案件都是社会危害性大、社会影响面较广的案件。因此，“7・5”案件由乌鲁木齐市中级人民法院作一审是法律明确规定的。<br />
<br />
　　记者：这次审判的7名被告人，全都构成故意杀人罪，对他们的定罪是否准确？<br />
<br />
　　陈奇彪：是否构成故意杀人罪，关键是要看行为人主观上是否有杀人故意，客观方面是否有杀人的行为，以及死亡结果与危害行为是否存在法律上的因果关系。<br />
<br />
　　对于什么是“犯罪故意”，我国《刑法》第14条规定：“明知自己的行为会发生危害社会的结果，并且希望或者放任这种结果发生，因而构成犯罪的，是故意犯罪。”从这三起案件的审理过程中可以清楚地看到，被告人犯罪性质极其严重、犯罪情节极其严重、人身危险性极其严重。例如被告人阿不都克里木・阿不都瓦伊提在对被害人陈艳军实施侵害过程中，持金属管钳朝陈头部连续猛砸15下，致陈当场死亡。被告人艾尼・玉苏甫等暴徒将面包车内的5人拉下车残酷殴打，其中4人受钝器打击头部、脑部，造成失血死亡。阿力木・麦提玉苏普等暴徒冲进“美乐休闲”足浴店用椅子猛砸两名被害人头部，致二被害人死亡。从今天法庭出示的被害人照片、视频看，众多被害人都是由于打头部致颅脑损伤死亡的。从打击的部位、打击的力度、所用凶器，充分说明这7名被告人是希望或放任被害人死亡结果发生的，具有杀人故意。从庭审证据看，被害人的死亡结果都是他们的行为所致，这7名被告人分别构成故意杀人罪，事实清楚，证据确实充分。<br />
<br />
　　记者： 我们注意到，今天乌鲁木齐市中级人民法院审判的7名被告人都触犯了两个以上罪名、法院对他们如何进行数罪并罚？<br />
<br />
　　茹克娅・霍加：这次审判的被告人都犯了两个以上的罪，其中犯有故意杀人罪、放火罪、抢劫罪等罪的较多。数罪并罚是指法院对一人犯数罪分别定罪量刑，并根据法定原则与方法决定执行的刑罚。数罪并罚的原则为吸收原则、并科原则、限制加重原则和综合原则。我国《刑法》规定，判决宣告数个死刑或最重为死刑(含死刑缓期执行)的，采用吸收原则，应当决定执行死刑，低于死刑的其他主刑不再执行。阿不都克里木・阿不都瓦伊提等被告人最高刑都判了死刑，所以执行死刑。<br />
<br />
　　《刑法》还规定，数罪中有判处附加刑的，如剥夺政治权利、罚金、没收财产，采用并科原则，即附加刑仍须执行。所以对7名被告人剥夺政治权利等附加刑仍须执行。<br />
<br />
　　记者：广大干部群众都很关心判决结果，今天审判的这三案7名被告人有6人被判处了死刑，我国《刑法》对适用死刑有哪些规定？<br />
<br />
　　陈奇彪：死刑包括死刑立即执行和死刑缓期两年执行两种情况，死缓不是独立的刑种，而是死刑的一种执行制度。对于死刑的适用，我国《刑法》从适用的条件作了限制性规定。《刑法》第48条规定：“死刑只适用于罪行极其严重的犯罪分子。”这表明，适用死刑的条件是犯罪分子所犯的罪行极其严重。所谓罪行极其严重，是犯罪性质极其严重、犯罪情节极其严重、犯罪分子的人身危险性极其严重的统一。这次判处死刑的6名被告人都是杀死多人、手段残忍的案犯，所杀害的对象有12岁的儿童，也有82岁的老人，还有妇女、残疾人。他们的犯罪行为，完全符合适用死刑的条件，对这批罪犯适用死刑，是符合法律的，是各族人民群众的强烈愿望，也是对死者及其家属的交代。<br />
<br />
　　记者：判处死刑为什么还要剥夺他们政治权利终身？<br />
<br />
　　茹克娅・霍加： 剥夺政治权利是剥夺犯罪分子参加国家管理和政治活动权利的刑罚方法。根据《刑法》第54条规定，剥夺政治权利的内容，是剥夺犯罪分子以下权利：选举权和被选举权；言论、出版、集会、结社、游行示威自由的权利；担任国家机关职务的权利；担任国有公司、企业、事业单位和人民团体领导职务的权利。<br />
<br />
　　根据《刑法》第56条、第57条的规定，必须附加剥夺政治权利的情况有以下两种：对危害国家安全的犯罪分子；对被判处死刑、无期徒刑的犯罪分子。对被判处死刑的犯罪分子附加剥夺政治权利终身，一是为了对其予以政治上的否定评价。二是为了有利于处理与罪犯有关的某些民事法律关系，例如他们的文章、著作不再享有发表与出版权等等<br />
<br />
　　记者：被告人塔衣热江・阿布力米提也参与了杀人，却被判无期徒刑，是出于什么原因？<br />
<br />
　　茹克娅・霍加：根据我国相关法律规定，犯罪分子有自首、立功表现的，可以依法从轻、减轻或者免除处罚。所谓立功，是指犯罪分子揭发他人犯罪行为，查证属实；或者提供重要线索，从而得以侦破其他案件等情况的行为。立功分为一般立功和重大立功。重大立功的主要表现形式为：揭发他人重大犯罪行为查证属实的；提供重要线索的，从而得以侦破其他重大案件；协助司法机关抓捕其他重要罪犯(包括同案犯)。<br />
<br />
　　李岩：塔衣热江・阿布力米提之所以被判无期徒刑，是由于其是案件的参与者，且归案后能主动坦白罪行，特别是他积极协助公安机关抓获重要在逃案犯，依法构成了重大立功，符合从轻处罚的情节。综合被告人塔衣热江・阿布力米提在犯罪中的地位、归案后的表现和重大立功的情节，乌鲁木齐市中级人民法院对塔衣热江・阿布力米提判处无期徒刑，是符合我国法律规定的。<br />
<br />
　　陈奇彪：是的，我们党和政府历来主张宽严相济的刑事政策，打击犯罪只是手段，教育挽救才是目的。因此，在处理各类违法犯罪过程中，始终坚持孤立打击极少数、教育争取大多数的原则。我认为，对塔衣热江・阿布力米提判处无期徒刑，充分体现了宽严相济的刑事政策，必然收到良好的法律效果、社会效果和政治效果。<br />
<br />
　　记者：这三起案件中的被告人的合法权利是否依法得到了保障？<br />
<br />
　　茹克娅・霍加：回答这个问题，还是用事实说话比较好。根据我国宪法、刑事诉讼法的有关规定，各民族公民都有用本民族语言文字进行诉讼的权利。人民法院、人民检察院和公安机关对于不通晓当地通用的语言文字的诉讼参与人，应当为他们翻译。在少数民族聚居或者多民族杂居的地区，应当用当地通用的语言进行审讯，用当地通用的文字发布判决书、布告和其他文件。从今天的庭审过程中看，三案7名被告人均是维吾尔族，参加法庭审理的全部是维吾尔族检察官和法官，而参加旁听的被害人亲属大都是汉族，法庭提供了维吾尔语―汉语同声传译，不仅确保了被告人和其他诉讼参与人的合法权益，而且也极大方便了其他参加旁听群众及时了解案情和庭审过程。<br />
<br />
　　陈奇彪：我国《刑事诉讼法》规定，被告人有权获得辩护，人民法院有义务保证被告人获得辩护。辩护律师的职责就是根据事实和法律提出证明被告人无罪、罪轻、减轻或免除处罚的材料和意见，以维护被告人的合法权利，保障司法公正，维护法律的正确实施。我们注意到，今天审判的7名被告人都有律师为其辩护，而且是本民族的律师。同时，从庭审中我们看到，7名被告人的其他权利，如申请回避的权利、自我辩护的权利、举证的权利、最后陈述的权利等等，也都依法得到了保障。<br />
<br />
　　李岩：事实上，9月25日公布的“7・5”事件第一批六案21名被告人中，据了解，有4名被告人委托律师进行辩护，17名被告人由法院为其指定了律师，从而充分保护被告人获得辩护的权利。<br />
<br />
　　不仅如此，鉴于“7・5”事件系列案件重大复杂，我们检察机关认真履行法律监督职责，抽调精干力量组成十余个监督小组，从侦查阶段开始对公安机关的侦查办案活动实施全程严格法律监督，并加强了监所检察力量，全天候对每个看守所进行了不间断检查监督。与此同时，我们在批准逮捕、移送起诉过程中，严格、慎重、细致地审查每一起案件。据我们了解，“7・5”事件侦查工作开始后，公安厅、乌鲁木齐市公安局的督察、法制部门组织20多个执法监督组，对侦查、羁押、审讯等执法活动进行了全面的内部监督。除此之外，法院、检察、公安机关都根据有关要求，在侦办、审理涉“7・5”案件中，实施全程录音录像，一方面确保证据的固定，另一方面防止执法过错。通过上述一系列的严格执法监督，确保了整个刑事诉讼活动程序公正、合法。</div>

							
							
							
							
