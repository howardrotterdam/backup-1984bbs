<br />
　　在称多县，地震发生数小时后，就选派以党员为主体，由医护人员、民兵应急分队、武警官兵、青年志愿者组成的１５０余人的抢险队，奔赴玉树救援。 <br />
<br />
　　在囊谦县、在曲麻莱县、在杂多县，迅速组建多支“党员突击队”，全力投入抢险救灾、医疗救治工作。 <br />
<br />
