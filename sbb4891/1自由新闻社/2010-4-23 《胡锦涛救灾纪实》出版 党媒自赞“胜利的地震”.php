　　几天来，他和同事们一起分发了２５０多顶帐篷。然而，他自己却没有领取一顶。他轻声说，“别人更需要”。 <br />
<br />
　　地震时，桑丁和周围的邻居有１９人被埋。从废墟中爬出来的桑丁，膝盖被砸，伤势严重，只做了简单包扎后，便战斗在抗震救灾战场上…… <br />
<br />
　　疼爱父母、疼爱妻子、疼爱儿女…… <br />
<br />
　　失去亲人瞬间，却从来没有停滞中国共产党人舍己为人的力量，因为他们有着一颗深沉而高尚的心。 <br />
<br />
　　更爱祖国、更爱人民、更爱事业…… <br />
<br />
　　悲切血泪涌出，却丝毫不能夺走中国共产党人心系苍生的信念，因为他们有着一颗博爱而伟大的心。 <br />
<br />
　　这，就是人民哺育的共产党人！ <br />
<br />
“剩下的日子，就是为党、为乡亲们做事。我的命是党给的，我可以为党死。” <br />
<br />
　　和人民同脉搏　与人民共舟济 <br />
<br />
　　走进玉树地震灾区，满眼临时搭建的蓝色救灾帐篷。只有才仁多加的白色游牧帐篷引人注目。 <br />
<br />
　　今年６１岁的才仁多加，是玉树县结古镇加吉娘社区二社社长。 <br />
<br />
　　在发放救灾物资时，才仁多加没有领救灾帐篷。那是因为才仁多加让灾区群众先领，而物资局就一直住在这顶从废墟中捡来的白色帐篷。他说：“我是一名共产党员。” <br />
<br />
　　走入这顶特殊帐篷，才仁多加躺在床上。病中的他还牵挂着社区里的特困户，吩咐家人给他们送去奶茶。他说：“我觉得我死不了，我做了一辈子的党员梦去年才实现，我要为党做的事还远远没有做完呢，要死也要为共产党死。” <br />
<br />
　　才仁多加与共和国同龄。从小便失去父母的他，在儿童福利院沐浴着党的关怀长大成人。 <br />
<br />
　　“进福利院的头一年，正好赶上自然灾害，大伙都没东西吃。院长不知道从哪里弄来一个地瓜，煮了满满一大锅‘地瓜粥’，把有地瓜渣的全分给我们这些无父无母的藏族娃娃，自己就喝清水。”才仁多加说。 <br />
<br />
　　就是这碗“地瓜粥”的恩情，让才仁多加坚定了永远跟党走的信念。 <br />
<br />
　　就在一年前，才仁多加终于实现了梦想――加入中国共产党。面对党旗宣誓的时候，才仁多加右拳紧握，老泪纵横。 <br />
<br />
　　“剩下的日子，就是为党、为乡亲们做事。我的命是党给的，我可以为党死。”才仁多加说。 <br />
<br />
　　在地震灾区，有许多像才仁多加这样的共产党人。他们很普通，普通得在迎面而过时都记不得他们劳累的面容。 <br />
<br />
　　在地震灾区，有许多像才仁多加这样的共产党人。他们很伟大，伟大得在遥望背影时便能洞悟他们崇高的境界。 <br />
<br />
　　吕国庆是玉树藏族自治州政府办公室副主任。地震中，他的家被夷为平地，６０多岁的老母亲腰部重度骨折。然而，地震发生不到１小时，吕国庆把老母亲托付给妻子，匆忙赶回单位。当天中午，妻子打来电话哭着告诉吕国庆，他的其他５个亲人都在地震中遇难了。 <br />
<br />
　　听到这一消息，吕国庆心如刀绞。“家人的后事，你替我料理吧。”吕国庆强忍悲痛，对爱人说完这句话，就立即投入紧张的救人救援中。４月１６日晚，已两天两夜没合眼的他，在伤员救治点工作时，看到了正在接受治疗的老母亲。原本以为老人家会怪罪他，没想到老人却说：“孩子，灾难当头，你放心去忙，不用记挂我。”吕国庆含着泪水，告别了母亲。 <br />
<br />
　　接连几天，吕国庆一直忙碌在灾区。他说：“我虽然不能到现场救人，但只要能动，就一定要坚守工作岗位。” <br />
<br />
　　“是个小男孩，母子平安！” <br />
<br />
　　４月１５日１６时２８分，青海陆军预备役步兵旅医院在灾区临时搭建的帐篷内，传来一声声新生婴儿的啼哭。一直守在帐篷外的孩子的爸爸看到小生命时激动地说：“谢谢，我太高兴了，我们全家永远会记住你们的。”孩子的妈妈也在产床上露出了笑容，向周围的人们竖起了大拇指。 <br />
<br />
　　此时，医生们还不及拭去额上的泪水，继续去接治新的伤员…… <br />
<br />
　　西宁市公安消防支队队长李海峰是消防战线上的老兵。地震发生后，他临危受命，率西宁消防支队抗震救援队７９名官兵火速赶赴灾区投入救援行动。 <br />
<br />
　　在玉树县城倒塌的结古寺宾馆余震不断，房顶缝隙不时窜出炙热的火苗。李海峰迅速勘查现场，发现两名群众被埋在坍塌房屋的狭小空间里，救人工作迅速展开。 <br />
<br />
　　突然，废墟下埋压的一只液化气瓶发生爆炸，李海峰和部分救援队员瞬间被掀翻在地。“同志们，上！”顾不及伤情，李海峰又和队员们冲上了废墟，经过２个多小时的奋战，被埋的两名藏族群众终于获救。 <br />
<br />
　　４月１５日１３时，在解放军第四医院抗震医疗队设立的野战医院里，被废墟掩埋了２９个小时、已经陷入昏迷的藏族小姑娘才让拉毛醒了过来。双眼蒙着纱布的她一把抓住输液的护士长宋海燕的手。哭喊着：“妈妈，妈妈！”宋海燕猛地怔了一下，赶紧红着眼圈连声答应：“孩子，别哭，妈妈在呢！”小姑娘平静了下来。 <br />
<br />
　　当宋海燕抱着脱离危险的才让拉毛交给她的母亲时，重新见到女儿的母亲一把抱住孩子，哭着说：“共产党真是‘活菩萨’”！ <br />
<br />
　　格尔木市与玉树山水相连。唐古拉山镇叶格牧委会主任助理白拉得知有伤员安置在格尔木市救治，急需陪护人员时，她主动请缨。白拉说：“虽然我们不能去玉树救灾第一线，但作为党培养多年的大学生‘村官’，能为灾区人民尽自己的绵薄之力，我觉得很光荣。” <br />
<br />
　　从那一刻起，白拉就时刻守候在病人身旁。 <br />
<br />
　　急群众之所急，中国共产党人甘愿宵衣旰食，为需要关爱的人们送去力量。 <br />
<br />
　　办群众之所需，中国共产党人甘愿呕心沥血，为需要关爱的人们送去温暖。 <br />
<br />
　　解群众之所难，中国共产党人甘愿殚精竭虑，为需要关怀的人们抚慰创伤。 <br />
<br />
　　这，就是人民哺育的共产党人！<br />
<br />
 “我是一名共产党员，多交纳一些党费，就能多为灾区渡过难关出一份力，这也是党员应尽的义务……” <br />
<br />
　　和人民同相依　与人民共担当<br />
<br />
　　灾情似火急，患难真情扬。来了，都来了―― <br />
<br />
　　从一笔笔多缴纳党费到一张张汇款单； <br />
<br />
　　从一辆辆驰向灾区的汽车到一个个赈灾义演的舞台； <br />
<br />
　　从一顶顶救灾帐篷到一件件御寒衣物； <br />
<br />
　　从一箱箱方便面到一瓶瓶矿泉水…… <br />
<br />
　　直面灾难，中国共产党人用真挚的情爱擎起未来。直击灾害，中国共产党人用坚强的臂膀托起希望。 <br />
<br />
　　青海尖扎县康杨镇西么拉村党支部书记王占华，在身患重病不能到捐款现场捐款的情况下，于４月１６日早晨专门委托别人将自己自愿多交纳的５０００元党费，送到了远在几十公里以外的尖扎县委组织部，以自己的实际行动支持灾区，表达了一名共产党员的高尚情操。 <br />
<br />
　　王占华原是康杨镇西么拉村的一名普通回族村民，他靠党的富民政策成了当地小有名气的实干家。２００２年，被村民一致推选为村党支部书记。 <br />
<br />
　　他说：“我是一名共产党员，多交纳一些党费，就能多为灾区渡过难关出一份力，这也是党员应尽的义务。” <br />
<br />
　　新疆福海县牧民党员阿不都热合曼・阿热依别抚养着包括哥哥嫂子过世后留下的孩子，虽然家境并不富裕。但是这位有着１４年党龄的党员专程骑马，从冬牧场赶到县委捐款５５０元。他说：“去年我们遇到雪灾，全国都帮助我们渡过难关，我不能忘了党的恩情。 <br />
<br />
　　哈萨克族有句谚语：‘房子四面墙都能牢固，才能遮风挡雨，房子才能温暖如春’，希望我们５６个民族就像四面墙一样永远牢固。” <br />
<br />
　　内蒙古五原县低保户白二仁，将５００元捐款交到工作人员手中说：“我的父亲是一名老党员，生前就要求我们多为党和国家作贡献，汶川特大地震时，我就替父亲交过一笔‘特殊党费’，现在我要替父亲再交一笔党费，希望能帮助灾区的人们……” <br />
<br />
　　君住江河源，我住江河边；同饮三江水，共有血脉情。 <br />
<br />
　　每一片衣襟都掖着春风的中国共产党人，把一缕缕温暖的阳光、一瓣瓣火热的心香播撒在雪域高原。 <br />
<br />
　　江苏扬州市江都喜洋洋公司青海分公司经理田野，第一时间组织了一支２０人的救援队，并采购了价值近２０万元的铁锹、矿泉水、药品、军大衣、５００床棉被等救援物资，由西宁出发驰援灾区。泰州市党员志愿者高丽从２００７年起，就与玉树县一家孤儿院结对，资助２０多名孤儿上学。当她得知这家孤儿院在地震中已成废墟，心急如焚，当即联系党员朋友募集到２万多元的善款，于４月１４日下午紧急赶赴西宁，将捐款转交当地红十字会，并主动加入到救援者行列。 <br />
<br />
　　４月１８日，玉树县结古镇胜利路。一支３２名农民志愿者组成的救援队伍正在紧张忙碌。这支特殊的队伍由抗冰义士宋志永率领，赶了两天三夜的路，从河北唐山来到灾区。 <br />
<br />
　　在这支爱心救援队伍中，除了有“感动中国”的１３位抗冰勇士外，其余大多数人是从附近８个村里报名的志愿者中挑选出来的，其中６人是共产党员。 <br />
<br />
　　４月１８日早上，星夜兼程的他们终于赶到结古镇，迅速投入到救援的行动。 <br />
<br />
　　君住江河源，我住江河边；同饮三江水，共有血脉情。 <br />
<br />
　　每一滴汗水都孕育真情的中国共产党人，把一缕缕温暖的阳光、一瓣瓣火热的心香播撒在雪域高原。 <br />
<br />
　　马宝祥，青海湟中县大才回族乡前沟村的一名共产党员。 <br />
<br />
　　４月１４日上午，由格尔木返乡途中的马宝祥得知玉树地震，立即与省红十字会取得联系，并提出由他自己负担抗震救灾队伍的全部费用、无偿提供自己的挖掘机、装载机等工程机械。省红十字会立即批准了他的请求，为马宝祥的抗震救灾小分队签署了赴灾区抗震救灾的公函、授予“红十字”会旗。１５日上午，马宝祥小分队８名人员到达玉树，接受救援任务后马上展开救援。 <br />
<br />
　　河北枣强县芍药村党总支书记王文忠率领的“芍药村党员抗震救灾突击队”一行１８人，经过４７小时艰苦跋涉，于４月１６日抵达玉树县结古镇。风尘仆仆的队员们顾不上路途辛苦，立即投入抗震救灾。当藏族妇女措毛从王文忠手中接过棉被和食品时，激动得泪流满面。<br />
<br />
　　西宁到玉树的距离８００多公里。其间，王文忠和队员们忍受着高原反应，既听不到一声怨言，更没有一人退缩…… <br />
<br />
　　这，就是人民哺育的共产党人！ <br />
<br />
　　今天，清溪流过的玛尼石堆可以见证―― <br />
<br />
　　越是在危难时刻，中国共产党人就越显示出中华民族中流砥柱的力量，受到各族群众衷心的信任和爱戴。 <br />
<br />
&nbsp;&nbsp;　今天，临风飘曳的五色经幡可以见证―― <br />
<br />
　　越是在危急时刻，中国共产党人就越能挺起不屈的脊梁，用实际行动抒写伟大的忠诚。 <br />
<br />
　　今天，碧草成茵的巴塘草原可以见证―― <br />
<br />
　　越是在危困时刻，中国共产党人就越能体现出特别能吃苦、特别能战斗、特别能奉献的英雄本色。 <br />
<br />
　　在地震中，在洪水中，在冰雪中，在干旱中……试问哪里的阴霾，没有中国共产党人托起的晴空？哪里的痛苦，没有中国共产党人悉心的呵护？ <br />
<br />
　　无论昨天是怎样的艰难，无论今天是怎样的危难，无论明天是怎样的困难，拥有人世间最美好最朴素情感的中国共产党人，用自己的鲜血和生命已经并将继续谱写一曲曲为人民服务之歌、为人民奉献之歌、为人民奋斗之歌。 <br />
<br />
　　这，就是人民哺育的共产党人！ <br />
<br />
　　中国共产党人永远和人民心心相系！ <br />
<br />
　　中国人民永远步伐坚定紧跟共产党！ <br />
<br />
　　胜利一定属于英雄的中国人民！胜利一定属于伟大的中国共产党人！</div>

							
							
							
							
