<br />
　　人们看到，使命在肩的中国共产党人正用自己的生命，捍卫这面鲜红旗帜的神圣。 <br />
<br />
　　人们看到，奔走在先的中国共产党人正用自己的鲜血，渲染这面鲜红旗帜的壮丽。 <br />
<br />
　　人们看到，冲锋在前的中国共产党人正用自己的赤诚，谱写这面鲜红旗帜的风采。 <br />
<br />
　　这，就是人民哺育的共产党人！ <br />
<br />
“不要哭，不要怕，我们要不惜一切尽快找到每一个亲人。” <br />
<br />
