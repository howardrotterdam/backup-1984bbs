													<h2>抗震救灾最前列的中国共产党人：永远和人民心心相系</h2>
						
						
																											<div id="postmessage_579588" class="t_msgfont"><a href="http://hxd.wenming.cn/ssjt/content/2010-04/23/content_115032_2.htm" target="_blank">http://hxd.wenming.cn/ssjt/conte ... ontent_115032_2.htm</a><br />
抗震救灾最前列的中国共产党人：永远和人民心心相系<br />
<br />
中国文明网　<a href="http://www.wenming.cn" target="_blank">www.wenming.cn</a>&nbsp; &nbsp;2010-04-23&nbsp; &nbsp; 来源: 新华社 <br />
【字号 大 中 小】　【打印】 　【关闭】<br />
 　永远和人民心心相系<br />
――记顽强挺立在青海玉树抗震救灾最前列的中国共产党人<br />
<br />
　　新华社青海玉树４月２２日电（新华社记者李亚杰 人民日报记者李章军） <br />
<br />
　　如果有谁，比慈祥的母亲还值得我骄傲和自豪， <br />
<br />
　　那就是你，历经风雨却愈益成熟的中国共产党。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;　　　　　　　　　　　　　――题　记 <br />
<br />
　　青海玉树，名山之宗、三江之源、康巴之乡。这里的雪山巍峨峻峭、雪莲纯洁无瑕、雪水晶莹透明。 <br />
<br />
　　２０１０年４月１４日７时４９分。美丽的玉树在大地无情的痉挛中猝然倒下，瞬间满目疮痍、转眼支离破碎，数以万计的高原儿女承受着撕心裂肺的苦痛。 <br />
<br />
　　这一刻，高原黑色之殇。银峰低垂在渴盼，江河挥泪在祈求，草原无语在怅望…… <br />
<br />
　　空中，穿破云雨冰雪。中国共产党人以第一速度赶来了，朝着目标――挺进！挺进！挺进！ <br />
<br />
　　陆上，踏着颤抖大地。中国共产党人在第一时间赶到了，凝聚力量――救人！救人！救人！ <br />
<br />
　　玉树在哭泣，每一个中国共产党人都在与时间争命。青海在震栗，每一个中国共产党人都在同死神角力。他们中间有干部，有人民子弟兵，有公安民警，有救援队员，有医生，有教师，有工人，有农民，有学生……他们中间有的在地震中痛失亲人，来不及抚平悲伤，通宵达旦抢送伤员；有的忍受着自身伤痛，坚守在工作岗位上；有的跋山涉水赶至玉树，忍受着高原反应的折磨，全力投入抗震救灾……中国共产党人选择了把一切都献给人民，以坚定的信念、无畏的气概、钢铁的纪律，成为抗震救灾的先锋队、灾区人民的主心骨、受灾群众的贴心人。　　站在阔远高原，走进神秘雪域。中国共产党人无论在哪里，就为哪里带来阳光和春风―― <br />
<br />
　　就像点燃的酥油灯，照亮高原所有的希望；就像灿烂的格桑花，绽放人间所有的祝福…… <br />
<br />
　　这，就是人民哺育的共产党人！ <br />
<br />
　　“在这一困难时刻，我需要尽快赶回国内，同我国人民在一起，投入抗震救灾工作。” <br />
<br />
　　和人民同甘苦　与人民共患难 <br />
<br />
　　玉树地震突袭而至，大地悲歌再起。 <br />
<br />
　　一个个鲜活的生命被废墟掩埋，一双双清澈的眼睛被泪水遮蔽，一片片城镇乡村被夷为平地。 <br />
<br />
　　灾情就是命令！中国共产党人的双脚就是春的萌动。 <br />
<br />
　　时间就是生命！中国共产党人的双手就是生的希望。 <br />
<br />
　　人民高于一切！中国共产党人的双肩就是爱的力量。 <br />
<br />
　　灾害临头，中国共产党人和人民心心相系。 <br />
<br />
　　“在这一困难时刻，我需要尽快赶回国内，同我国人民在一起，投入抗震救灾工作。”正在国外访问的胡锦涛主席果断决策压缩出访行程提前回国。 <br />
<br />
　　４月１７日１４时２０分许，经过近２５个小时长途飞行后，胡锦涛总书记回到北京。１６时，立即主持召开中央政治局常务委员会会议，全面部署抗震救灾工作。 <br />
<br />
　　４月１８日，抗震救灾关键时刻。胡锦涛总书记飞抵玉树地震灾区。在扎西大同村、在玉树州体育场、在扎西科村……总书记的手与灾区群众牢牢握在一起，总书记的心与灾区群众紧紧贴在一起。 <br />
<br />
　　“我相信，只要我们发扬伟大抗震救灾精神，团结一心，众志成城，就一定能够夺取抗震救灾斗争的胜利！”胡锦涛总书记坚定铿锵的话音，在高原上空久久回荡。 <br />
<br />
　　吴邦国、温家宝、贾庆林、李长春、习近平、李克强、贺国强、周永康等中央领导同志情牵玉树震区、心系灾区人民。 <br />
<br />
　　灾害临头，中国共产党人和人民手手相挽。 <br />
<br />
　　中共中央组织部４月１４日、１５日连续发出通知，要求灾区各级组织部门在党委领导下，迅速动员和组织广大基层党组织和党员干部立即投入到抗震救灾工作中去；要求灾区各级党委、基层党组织和广大党员坚决贯彻党中央、国务院的决策部署，全力做好现场救援、灾民安置、社会维稳等各项工作，在抗震救灾中充分发挥领导核心、战斗堡垒和先锋模范作用。同时，连续两次给青海省下拨党费共５００万元，送去了党中央对地震灾区的殷切关怀。 <br />
<br />
　　青海省委、省政府在第一时间作出地震应急一级响应，组织动员全省各级党组织和党员干部投入抗震救灾工作，以最快的速度全力抢救被困群众，救治受伤人员，充分发挥领导核心、战斗堡垒和先锋模范作用。 <br />
<br />
　　青海省委组织部快速响应、迅速行动，健全和新成立受灾群众安置点党组织，在抗震救灾一线发展党员，抽调６０名党员领导干部赶赴灾区，把党的旗帜打出来。 <br />
<br />
　　１小时、１分钟、１秒钟…… <br />
<br />
　　服从党的领导和人民的利益，中国共产党人紧急行动，冲锋在抗震最前列，筑起震不垮的坚强堡垒。 <br />
<br />
　　１秒钟、１分钟、１小时…… <br />
<br />
　　听从党的指挥和人民的召唤，中国共产党人紧急救援，战斗在救灾最前沿，筑起压不塌的战斗堡垒。 <br />
<br />
　　在房倒屋塌的村庄里，一个个挺身而出的共产党人、一个个迎难而上的共产党人集聚起来―― <br />
<br />
　　他们抖去满身尘土、擦去伤口血迹，来不及料理亲人的后事，动员组织群众搏斗灾难，在灾情最严重的地方挽救危亡中的生命，在灾害最危险的地方寻找煎熬中的幸存者。 <br />
<br />
　　在断瓦残垣的废墟前，一个个舍生忘死的共产党人、一个个百折不挠的共产党人冲锋在前―― <br />
<br />
　　他们从四面八方而来，与当地党员干部和群众一道，以寻找生还者为最大目标，一次次细致的搜索、一遍遍拉网式勘查，全然不顾剧烈的高原反应，浑然不觉寒冷与饥饿。 <br />
<br />
　　在灾情最严重的地方，一个个愈挫愈奋的共产党人，一个个愈战愈勇的共产党人勇不退缩―― <br />
<br />
　　他们克服余震威胁，主动承担起最艰苦的工作，深入村组、农户，安抚群众、搜集灾情、排查隐患，组织群众开展自救，把党的温暖送到每户受灾群众。 <br />
<br />
　　在受灾群众的帐篷中，一个个有血有肉的共产党人、一个个有情有义的共产党人热血沸腾―― <br />
<br />
　　他们视受灾群众为亲人，为灾区群众送来水米充饥、拿来衣被御寒、捧来爱心送暖，确保有饭吃、有衣穿、有水喝、有帐篷住、有病能及时医治。 <br />
<br />
　　“党员突击队”“党员救援队”“党员应急小分队”“党员交通抢险队”“党员医疗救援队”…… <br />
<br />
