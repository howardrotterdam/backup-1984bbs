<br />
 日本时事周刊《周刊文春》在6日发行的新年特刊中刊登题为《崔智友是美国佬女王？》的篇幅达两页的文章，恶意中伤崔智友说：“她在韩国是‘丑闻女王’，与在日本走红的清纯派形象完全两样。” <br />
<br />
 这篇文章是观照37名日本女性名流隐私的特别报道的一部分，在崔智友篇标题所谓的“美国佬”是一种日本俗语，指善于嬉戏的作风不太正派的人。除了崔智友之外，网球明星沙拉波娃和日本女歌星松田生子等也同样遭到了攻击。 <br />
<br />
该文章说，继裴勇俊之后，崔智友在日本被称为“智友姬”成为当红明星，受到了十几个企业的广告邀请，但因为她要求1亿日元(约合10亿韩元)的天价身价难以成事。文章还引用生活在韩国的记者和作家的话说，崔智友每出演一部电视剧都和在日本也人气很旺的A、B某等男演员爆出了丑闻，一度曾与身为政府要职的C某也有过某种关系，并提到了4名男演员和一名政治家的实名。文章还说，崔智友发音不准是整容手术的后遗症，如此崔智友在韩国不像在日本被认为“清纯派”明星，而是另一种样子，所以没有受到女性观众的欢迎。 <br />
<br />
 《周刊文春》是与代表日本的时事月刊《文艺春秋》一起由大型出版社文艺春秋社发行的杂志。被认为是代辨日本保守派的文艺春秋社曾在没有得到韩流明星同意的情况下出版韩国摄影师拍摄他们的写真集，成为问题。 <br />
<br />
<br />
 对此，崔智友所属的SIDUS HQ公司方面表示：“今年还有很多韩日友谊年相关的活动，真让人难过。7日上午，已经通过日本分公司向《周刊文春》方面传达了有关我方要求的书函。” <br />
<br />
 SIDUS要求该周刊的发行公司文艺春秋方面在日本的主要日刊第1版面刊登更正报道和致歉信，并决定，如果对方不接受该要求，将以损毁名誉为由采取强力应对措施。另外，还计划向日本国内的韩国大使馆寻求协助。 <br />
<br />
 另外，文化观光部的一位相关人士就此事表示：“目前还没有看过报道内容。25日和27日将分别在东京和汉城举行韩日友谊年40周年纪念活动，此时出现这样的报道，真是令人感到非常遗憾。”</div>

							
							
							
							
