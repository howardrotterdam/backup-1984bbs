																											<div id="postmessage_304002" class="t_msgfont">2009年10月28日 <br />
<br />
下月9日是柏林墙倒塌20周年，德国官方将举行一系列纪念活动。出人意料的是，为纪念这一日子而设立的「网络柏林墙」（ <a href="http://www.berlintwitterwall.com/" target="_blank">http://www.berlintwitterwall.com/</a>），近日已被中国网民攻占，要求推倒「 GFW」（ Great FireWall的缩写，即中国官方进行网络审查的「长城防火墙」）的中文留言铺天盖地。 <br />
<br />
声讨「 GFW」网络审查 <br />
<br />
「网络柏林墙」设立的本意正是让网民分享对柏林墙倒塌的感想，并指明还有那些墙必须推倒才能让世界更美好。结果，饱受GFW之苦的中国网民群起响应，自上周五开始，「网络柏林墙」俨然成了声讨GFW的中文留言板，被称为GFW之父的北京邮电大学校长方滨兴也因此「名扬」西方。 <br />
<br />
中国网民攻占「网络柏林墙」，受到西方传媒关注。 《柏林早报》（ Berliner Morgenpost）引述「网络柏林墙」的创办人海因（ Carsten Hein）表示，对中国网民的活跃感到既吃惊又高兴，「我们很高兴能够成为来自中国的民众抗议活动的平台。」加拿大《环球邮报》（ The Globe and Mail）报道，自周一晚开始，「网络柏林墙」在中国已被官方封锁，该报呼吁中共总书记胡锦涛拆除防火墙。 <br />
<br />
值得一提的是，中国第一次与外界的网络沟通，正是在北京与德国之间进行。中国互联网资讯中心的资料显示， 1987年9月，北京市电脑应用技术研究所向德国学术机构发出了中国的第一封电子邮件，这封邮件写着「越过长城，走向世界」，以彰显中国研究人员的雄心壮志。 <br />
<br />
「长城」阻网民走向世界 <br />
<br />
讽刺的是，中国科研人员在研究网络技术上的最大成功，竟是筑成了阻碍中国网民走向世界的防火墙「长城」。在中国民间传说中，孟姜女哭倒长城的故事凄凉悲壮。那么，在网民的怒吼声中，长城防火墙还要多久才会倒塌？</div>

							
							
							
							
