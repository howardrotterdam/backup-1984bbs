													<h2>《仰望星空》正式确定为北航校歌</h2>
						
						
																											<div id="postmessage_613243" class="t_msgfont">总理诗作结合优美旋律 深邃意境构筑精神华章&nbsp;&nbsp;<br />
日前，经学校党委常委会研究决定，北京航空航天大学校歌正式确定为《仰望星空》。校歌选择温家宝总理的诗歌作品《仰望星空》为歌词，邀请沈阳音乐学院的艺术家进行音乐创作。校歌的确定标志着北航的精神文化体系进一步完善，逐步形成了以北航精神、办学理念、校训、校风、校歌等为核心的文化传统及创新特质。<br />
<br />
<br />
去年底，学校第15次党代会正式提出了“建设空天信融合特色的世界一流大学”的愿景，要实现这一新的远景发展目标，需要积极、厚重、深远的精神文化做支撑。近年来，我校的文化建设工作取得了显著成效。在精神文化建设方面，学校正式确立了北航精神、办学理念、校训、校风等，成为广大师生和校友的理性共识与自觉行为，受到大家的高度认同。校歌作为反映大学人精神风貌和品格的一种艺术表现方式，也是大学精神文化建设的重要内容。自2006年起，学校在师生和校友中广泛开展了校歌征集活动，涌现了一批词曲佳作，对于增强师生校友的凝聚力、责任感和归属感，发挥了重要作用。<br />
<br />
<br />
《2010年北京航空航天大学工作要点》提出，要将创作确立校歌作为丰富学校精神文化内涵、推进学校文化建设的重点任务。集中近年来广大师生、校友的意见，大家提议并希望将温家宝总理2007年9月4日发表在《人民日报》的诗歌《仰望星空》作为北航校歌的歌词。因为，北航师生对温总理始终怀有一种特殊的爱戴之情，都铭记着他两次莅校亲切看望和勉励大家的情景：2003年非典时期，总理来到北航，在主楼的教室里对师生提出了“中国要制造自己的大飞机”的殷切期望；2008年12月金融危机席卷全球时，总理又一次来到北航图书馆，和同学们亲切座谈，鼓励大家“树立信心”，要依靠知识和科技的力量，勇于承担国家重任。《仰望星空》这首诗，意境阔大而深邃，格调宁静而致远，读起来令人动容、回味无穷，特别是对北航的建设和发展具有特殊而深刻的意义，将激励北航人树立崇高理想和远大志向，激发北航人服务国家战略需求的科学创新精神和人文情怀，为实现空天信融合特色世界一流大学的目标而不懈奋斗。<br />
<br />
<br />
《仰望星空》谱成曲后，旋律优美感人，节奏流畅明快，深沉大气、使命感强、催人奋进，具有很强的感召力和凝聚力，实现了音乐艺术与诗歌艺术的完美结合，很好地融合了学校的文化传统与精神特质。在北航校歌的确定和艺术创作过程中，教育部鲁昕副部长、沈阳音乐学院刘辉院长等领导给予了大力支持。2010年4月26日，杜玉波书记、怀进鹏校长代表全校师生给温总理写信并附演唱光盘，总理阅知了北航师生确立校歌的夙愿。5月4日，《仰望星空》这首歌曲在学术交流厅举行的“冯如杯”学生学术科技作品竞赛20周年庆典活动中，由北航大学生艺术团正式演唱，并配以诗朗诵，受到师生和来宾的肯定与欢迎。<br />
<br />
<br />
北航校歌《仰望星空》的确定，是学校精神文化建设中的一件大事。近日，学校党委就深入开展学唱校歌活动进行了部署安排，要求全校各单位和广大师生员工各单位要高度重视，充分认识深入开展学唱校歌活动重要性，切实加强领导，统筹安排部署，抓好工作落实；要加强对学唱校歌活动的宣传，充分利用校报、广播、电视、网络等舆论阵地，大力宣传、传播校歌的丰富内涵，为活动营造良好的舆论氛围；各分党委（党总支、直属党支部）、学生工作部、研究生工作部、工会、团委要通过各种形式，以主题晚会、演唱、演奏、朗诵和舞蹈等艺术方式，传唱和表现校歌的内涵，按照尽快让每一名师生对校歌做到耳熟能详的要求，创新形式，注重实效，在全校师生中深入开展学唱校歌活动，切实将学唱校歌活动组织好、贯彻好。<br />
