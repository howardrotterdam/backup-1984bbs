元旦游行申万人参与 <br />
<br />
【明报专讯】社民连和公民党下周日（1月3日）会举行记者会，公布5名议员辞职的日期和五区总辞的各项细节，而民主动力和泛民主派将于本周五元旦当天发起「还我普选」元旦大游行，由中环遮打行人专用区游行至西环中联办。对于日前有示威者在中联办大闸绑上黄丝带时被中联办职员剪伤，民主动力指出，元旦示威者会再次绑黄丝带，呼吁中联办要派代表出来接收请愿信。<br />
<br />
公民党秘书长陈家洛表示，继元旦大游行后，他们会于紧接的星期日召开记者会，公布总辞的日期和各项细节。被问到议员是否在1月27日正式辞职，他没有否认，只说「容后公布」。<br />
<br />
周日公布总辞细节<br />
<br />
民主动力召集人蔡耀昌在于铜锣湾行人专用区召开论坛和记者会，表示已向警方申请1万人参加今次游行，并已收到警方的不反对通知书。蔡耀昌表示，难以预计游行人数，只表示会有「相当市民参与」。他指出，是次游行终点在中联办，是要向中央政府表达港人的普选诉求。<br />
<br />
游行路线会由遮打道行人专用区、经雪厂街进入皇后大道中、再经皇后大道西西行至朝光街，然后游行至德辅道西行人路，到达中联办后门对面，示威者会于后门闸绑上黄丝带，大会会于中联办后门对面的行人道搭建讲台，警方将开放德辅道西行线其中一条行车线供游行人士使用。<br />
<br />
拟再绑黄丝带 促勿诉诸暴力 <br />
<br />
对于日前示威者到中联办抗议内地维权人士刘晓波被判处11年监禁，在中联办铁闸绑上黄丝带时遭中联办职员剪伤，引发双方冲突，蔡耀昌指中联办并非私人地方，中联办公然剪伤示威者不可接受，呼吁中联办派人出来接信。<br />
<br />
民间人权阵线昨日在旺角行人专用区呼吁市民参加元旦大游行，民阵政制小组召集人李耀基表示，希望警方能保障示威者的示威权利，确保中联办不会用粗暴方式对待示威者。<br />
<br />
泛民议员伙拍民主动力，昨日与民阵分别在铜锣湾和旺角举行街头论坛及记者会。两方被问到是否因不和而「分家」，民主动力和民阵均表示没有不和或分裂。据悉，民阵与其它民间团体认为，每次有政党参加这些街头论坛，传媒都只集中在政党和议员身上，有感自己的意见未获注意，民阵宁愿各行在旺角举行活动，呼吁市民上街。<br />
<br />
<br />
<br />
民主动力召集人蔡耀昌表示，中联办职员公然刺伤示威者的做法不能接受，并指元旦大游行会继续在中联办大闸绑上黄丝带，呼吁中联办派代表接信。（何家达摄）</div>

							
							
							
							
