红旗、红海昨天淹没港岛区，国庆旗海越红，令浸会大学学生会前会长叶楚茵更觉祖国越来越黑。「你睇到许志永等维权人士，关心三鹿奶粉问题都俾公安带走，国庆令我好心酸！」十．一当日，个别大专院校的学生会成员，将出席国庆酒会。叶楚茵则决定到时代广场外，参与 60小时绝食静坐。民主党葵青区议员梁国华也会参加绝食，他说建政 60年，本应开心庆贺，但中国至今仍未见民主，没有自由；加上今年正值June Fourth 20周年，令他倍觉心痛。「谂起革命前辈为追求民主奉献；到今日，民主人权依然系交白卷！」支联会副主席蔡耀昌呼吁，所有真正关心国家、真正爱国的市民十．一上街。<br />
