&nbsp;&nbsp;<br />
【本报讯】 60年前，毛泽东在天安门城楼上宣布中华人民共和国中央人民政府成立；从此中国陷入反右、大饥荒、文革、June Fourth屠城、贪污腐败、毒食品横行、维权人士被镇压等灾难，逾一亿人死于各种政治运动。 60年后的今天，支联会质问「人权何在？中国人民站起来了吗？」他们首次在十．一发起千人大游行及 60小时静坐绝食，希望在一片歌舞升平中，提醒当权者反思。记者：雷子乐<br />
<br />
<br />
支联会昨在旺角街头宣布一系列十．一抗议活动。支联会主席司徒华表示， 60年前中共只是建政，共产党一党专政，压迫人民，与自由、民主背道而驰。建政首 30年的三反五反、镇反肃反、鸣放、反右运动、大饥荒、文革、四五天安门事件等政治运动，造成无数人命伤亡，大饥荒最少饿死 3,600万人，反右、文革期间有 7,000万人是非自然死亡。<br />
<br />
逾 20人绝食 60小时 <br />
78至 89年虽已提出改革开放，但邓小平提出的「四个坚持」（即坚持马列毛主义、坚持社会主义道路、坚持无产阶级专政、坚持共产党领导），是延续独裁专政，导致「June Fourth」悲剧。 89至 09年贪腐问题严重、爆出豆腐渣丑闻、维权人士被无理拘禁打压。司徒华说：「究竟 60年来，人权何在？」支联会昨宣布在 10月 1日，发动千人黑衣游行，由中区遮打道前往西环中联办绕圈请愿。此外， 20多人已报名参加 10月 1日起在铜锣湾时代广场外举行的 60小时静坐绝食。当晚 8时更会举行烛光集会。 10月 3日中秋节，支联会更会在时代广场外举行灯火会及公众论坛。<br />
<br />
