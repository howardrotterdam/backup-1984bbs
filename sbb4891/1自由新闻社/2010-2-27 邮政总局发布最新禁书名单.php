													<h2>《怎么看和怎么办》</h2>
						
						
																											<div id="postmessage_496474" class="t_msgfont">发布: 2010-1-17 00:24 | 作者: 张勤德 | 来源: 本站原创<br />
<br />
怎 么 看 和 怎 么 办<br />
 <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;张勤德着&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;<br />
&nbsp; &nbsp;&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;目&nbsp; &nbsp;&nbsp; &nbsp;录 <br />
09建言<br />
新疆事件的政策反思<br />
&nbsp; &nbsp; ----兼答对《09建言》的一些批驳<br />
《建言》为什么不采纳这些意见<br />
 打好“批万(里)战役”的三条建议<br />
 对温家宝总理的六条意见<br />
 人人都应提问和回答的七大问题<br />
&nbsp; &nbsp;&nbsp;&nbsp;――对30年改革“十大失误”原因和责任的追问<br />
 后记<br />
 <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;0&nbsp; &nbsp;9&nbsp; &nbsp;建&nbsp; &nbsp;言<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; <br />
拆除政治防火墙是“建言”和“宪章”决战胜负的关键――张宏良关于《09建言》的按语&nbsp;&nbsp;<br />
<br />
后面所附甄卫国先生的文章《09建言》，是针对《08宪章》发表的一份“真共产党人宣言”。针对《08宪章》要推翻共产党、解体共和国，把中国变成西方国家经济殖民地和政治管辖区的战略目标，甄卫国先生提出了真假共产党的应对战略，即通过共产党内部革新，把祸国殃民的假共产党员清除出去，恢复共产党的阶级性质和政治路线，把共产党和人民群众再次联系在一起，重新夺回人民失去的一切，避免国家分裂，维护民族统一和国家安全。如同《08宪章》在西方国家和国内右翼势力之间已广泛流传一样，据说这篇《09建言》也在许多关心党和国家命运的干部群众之间广泛传阅，并且传阅的层次并不低于《08宪章》。《08宪章》已在共产党内确认了自己的政治领袖，这已成为除鸵鸟之外众所周知的公开事实，《09建言》是否也象《08宪章》那样能够得到党内高层支持，暂且不得而知。但是无论怎样，《08宪章》和《09建言》的针锋相对，表明决战双方已悄然进入了各自的战壕，决战胜负的关键取决于共产党和人民群众之间那道政治防火墙。<br />
<br />
《08宪章》的优势，在于他们在共产党和人民群众之间设立了一道政治防火墙，通过30多年持续控诉所谓文革暴行，把共产党和人民群众从政治上完全隔离开来，再通过打着改革旗号抢劫和剥夺人民群众的方法，不断巩固和加强这道政治防火墙，最后再通过把全部罪行都推到共产党头上的办法，把人民群众的愤怒和仇恨全部引向共产党，在突发事件中把共产党彻底孤立起来，让中国陷入动荡和分裂，为外来势力干预中国创造条件。这是他们自信能将共产党和共和国置于死地的“葵花宝典”。《09建言》的优势，在于要想办法拆除掉对方设立的这道政治防火墙。而拆除这道政治防火墙的唯一利器，就是三个字：毛泽东。国内外敌对势力深深懂得这一点，正在掀起新一轮反毛狂潮；中国人民也深深懂得这一点，正在向毛泽东的旗帜自发地聚拢。能否再次高举毛泽东的旗帜，能否拆除右翼势力设立在共产党和人民群众之间的政治防火墙，是双方决战胜负的关键因素。<br />
<br />
