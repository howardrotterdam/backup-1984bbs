关于改革开放。一位农民工说：“改革就像给伤风感冒的人治病，当然需要，但像对待敌人那样往死里整，就不是改革而是改道(改变方向道路)了”。这表明，搞好改革不仅要肯定社会主义是个需要通过改革不断向前发展的社会即必须走改革之路，而且必须以四项基本原则为标准区分社改观和资改观，必须通过拨乱反正防止改革背离自己“社会主义制度的完善和发展”的本质，即在改革工作中解决好改什么、怎么改、靠谁改的问题，防止社会主义改革演变成补课式改革、西化式改革。<br />
<br />
关于四项基本原则。这四个坚持本身就意味着坚持阶级斗争、坚持革命。认为在社会主义初级阶段资产阶级不仅消灭了而且永远不会再产生，就从根本上否定了发生和平演变、资产阶级夺取政权的任何可能牲，是一种变相的“阶级斗争熄灭论”。受这种谬论影响不讲四项基本原则的革命性、实践性和对经济建设的动力作用，用改革代替甚至否定革命，便会使这些原则实际上变成套话、空话。<br />
<br />
关于中国特色社会主义。社会主义是坚持公有制和按劳分配、人民民主专政和党的领导、马克思主义指导地位的基本制度，以完成彻底消灭剥削和压迫、解放和发展生产力、实现共同富裕、推动社会全面进步和科学发展，为过渡到共产主义创造条件的历史任务的社会。公有制和按劳分配的主体地位，是区分中国特色社会主义和中国特色资本主义的试金石。如果改掉并且不恢复这种主体地位，就是改旗易帜、乱折腾。<br />
<br />
民主、自由、人权，是西方国家对社会主义国家进行和平演变、发动“颜色革命”一直在打的三个旗号，是《08宪章》的核心内容、西化派的主要理论观点。“庸俗生产力论”、“阶级斗争熄灭论”，是把初级阶段变成补课阶段的依据即西化派更深层次的理论根据。我们只有驳倒这些谬论，才能克敌制胜。&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;<br />
&nbsp; &nbsp;&nbsp; &nbsp; 三、把打垮西化派作为现阶段的第一战略目标<br />
<br />
我们的最高纲领，是实现共产主义；最低纲领或者叫现阶段的战略目标，主要有以下三个：<br />
<br />
首先，彻底打垮西化派。为什么要把它作为第一战略目标呢？一是因为，西化派是当前最凶恶的敌人。该派的核心赵紫阳集团就表现出了这一点。大量事实证明，在包括补课派在内的右翼各集团中，这个集团最反动。他们为了自身的私利，公然要使中国倒退到满洲国那样的殖民地社会，这还不够反动吗？这个集团最疯狂。他们不仅敢于公开为赵紫阳翻案(《多维新闻网》2009年4月10日报道，最近李锐、胡绩伟等12位赵紫阳的老部下和亲信在香港联名出书，呼吁“为赵紫阳平反，以后逐渐平反「六四」”)，而且公开煽动反革命暴乱和军事政变，大讲要“以暴力克服暴力”；“军队里面也有明白人，如他们认为不能通过民主的方法求得社会的进步实现，就可能发生军事政变。”(谢韬，《在成都座谈会上的讲话》，《乌有之乡》2007年4月27日)这个集团目前权势最大。由于国际上敌强我弱的总态势依然存在；官僚买办一直得到西方国家的有力配合和支持；外资已在相当程度上控制着我国经济；相当多的中高级干部通过在西方国家培训等形式被洗脑，一部分已成为“黄皮白心的香蕉人”；不少高级干部、高级知识分子身在右中不知右，只反“左”不反右，甚至把社改派当作“极左”残酷斗争、无情打击。再加上崇洋媚外、物欲横流、有奶便是娘的经济实用主义、各人顾各人的一盘散沙状态、反对西化派也被当作破坏社会稳定者受到专政，以及民族资产阶级既有反帝的一面又有依附列强的一面等等，都使他们处于强势地位。能够保证“新西山会议”的策划者、2007年1月在《炎黄春秋》上宣扬“只有民主社会主义才能救中国”的谢韬等人至今安然无恙；能够使全国几乎所有宣传工具都于2007年2月27、3月15日O载宣扬普世价值的长文或在“两会”后的答中外记者问等等，就是明证。<br />
<br />
 二是因为，这最有利于揭露资改派的丑恶面目。在资改派中，是存在着补课派和西化派两个派别的，使西化派充分暴露投敌卖国、甘当洋奴的汉奸本质，他们便成了过街老鼠。因此，把打垮西化派作为突破口，最符合毛主席的策略思想。 <br />
<br />
 三是因为，这样才能形成以中国共产党为领导核心，包括民族资产阶级、港澳同胞、海外侨胞中的一切爱国者在内的广泛爱国统一战线，最大限度地壮大自己、孤立敌人。<br />
<br />
一些同志完全反对这些主张，认为即使在当前西化派发动“决战”的情势下，突出抓一下官僚买办资产阶级和无产阶级及人民大众的矛盾，也是否定社会主义时期资产阶级和无产阶级这一主要矛盾；认为资本主义复辟已全面完成，必须响应西化派“联合左派，打倒政府”的号召，共同推翻现行的政治制度；认为共产党已彻底变质，不仅不应再维护它的执政地位，反而应通过实行西方国家的多党制以便把它赶下台。可见，这实质上是一种和认为搞社会主义革命便要依靠工人在城市武装暴动，不能走农村包围城市道路的王明左倾机会主义相类似的错误倾向。如果我们按这种主张办，就会失去一次用党内社改派和人民群众相结合的方式解决问题的历史机遇。但由于它是以比谁都革命的面目出现的，迷惑性特别大，只有下特别大的功夫纠正，才能防止其误导群众。<br />
<br />
其次，是在战胜资改派的基础上，逐步恢复公有制、按劳分配的主体地位；计划经济为主、市场调节为辅的运行机制；独立自主，立足自力更生、平等互利的对外开放方针；鞍钢宪法的基本精神；在城乡对发展集体合作经济组织的积极引导；以满足人民日益增长的物质文化需要为社会主义生产目的；对私有制经济的“利用、限制和改造”政策，等等。在政治上全面恢复党的工人阶级先锋队性质；真正坚持工人阶级领导的、以工农联盟为基础的人民民主专政；工农商学兵的代表在各级人大代表中所占的多数地位，等等。在思想文化上全面恢复马克思主义在意识形态领域的指导地位；全党全国人民的马列主义、毛泽东思想大学习活动；爱国主义、集体主义、社会主义的主旋律地位；人民群众在主流媒体批判敌对阶级及各种反动、错误思潮的权利；文艺为人民大众，首先为工农兵服务的方针，等等。在社会建设上全面恢复国家和单位免费或低价提供福利住房；全民义务教育；全民医疗养老福利和把医疗卫生工作的重点放到农村去的方针，等等。<br />
<br />
再次，是以按照马克思主义基本原理和中国实际情况相结合的原则所建设的毛(泽东)式科学社会主义为基础，汲取建国以来正反两方面的经验，借鉴古今中外一切对我有益的东西,不断开创新局面，积极创造条件实现社会主义事业的伟大复兴。<br />
<br />
毛主席带领全党全国人民对在中国这样一个东方大国里如何建立、巩固、发展社会主义，进行了27年极其艰难曲折的探索。这一时期取得重大成绩的根本原因之一，是把服务人民作为我们各项工作的根本目的，把人民群众作为事业发展的根本动力，把人民拥护不拥护作为衡量工作的根本标准。苏东剧变中特别令人震撼的一个教训，是82％以上的人民群众拥护社会主义，却由于高级干部中76％的人主张走资本主义道路而导致了复辟倒退。这就充分暴露了戈尔巴乔夫式民主社会主义的权贵资本主义本质。因此，我们一定要站在新的历史高度，更深刻地认识毛主席关于人民民主是我们避免陷入政权兴亡周期律的新路；为人民服务是党的宗旨；合乎人民利益得到群众拥护是衡量我们工作的最高标准；人民才是创造历史的动力；兵民是胜利之本的论断，实质上是提出了新民本主义观。把新民本主义作为不断开创新局面的社会主义的重要特征，才最有利于战胜权贵资本主义。我们按照新民本主义的要求进行政治体制改革和政治建设，必然会创造出在形式、实质上都远远高于西方民主的新型社会主义民主。与此相联系，在经济、文化、社会管理体制改革和建设方面，都要有新的进展。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;四、抓好打垮西化派的各项准备工作<br />
<br />
（一）当务之急是使更多的人充分认识近期发生“决战”的危险性。一些国家的西化派得以搞成“颜色革命”，和善良的人们容易轻敌轻信是有关系的。“八九风波”得以搞大，也有类似之处。认识不到危险是最大的危险，轻敌轻信的失误是最可悲的失误。这在于它会使我们自我放弃本来战无不胜的力量。务必要以王佐断臂之决心，撤换一批处于软弱涣散状态的高级干部，增强“一失策成千古恨”的忧患意识，以确保万无一失。<br />
<br />
（二）真正解放思想，搞清群众最不满意问题的产生原因。拿腐败来说，在建国的前30年里这类问题就比较少；在他们认为实行多党制最成功的美国，仅在最近便发生了“伊拉克重建工程中美军500亿美元的贪腐窝案”(《报刊文摘》2009年3月13日)之类最大的腐败现象。把这类问题搞清讲明了，政治骗子的日子就不好过了。<br />
<br />
（三）坚决反击“砍旗运动”。近年来补课派尤其是西化派，通过大造“我们只能举中国特色社会主义的旗帜而不能举别的旗帜，而中国特色社会主义不包括毛泽东思想”的与论，搞起一场新的大砍毛泽东思想伟大旗帜的“砍旗运动”。宪法明确规定的四项基本原则里，强调必须坚持马列主义、毛泽东思想。可见砍旗是违反宪法的，我们要通过“护宪运动”反击“砍旗运动”。现在，工农商学兵和共产党员的大多数，经过对正反两方面实践经验的反复思考和总结，更深刻地认识到只有毛主席的路线、方针、政策，才能解决群众最不满意的腐败增多、分配不公、治安恶化、国资流失等问题；保证工人阶级的领导地位和人民群众的国家主人地位；搞好反和平演变斗争；使全国人民共同富裕；维护住国家的独立主权和民族尊严；建设一个全心全意为人民服务的党。一句话，只有毛式科学社会主义才能救中国。砍毛泽东思想红旗，就是砍劳动人民的命根子，他们正在和必定会进行更大的反击。真正的共产党员应参与并领导这种斗争。<br />
<br />
(四)要喊响爱国、清正、平等、进步的口号，开展反卖国、反腐败、反特权、反倒退的斗争。必须广泛深入地发动群众揭发、批判西化派的下列卖国行为：一是把大量的国有资产贱卖给外商，“仅十多家银行由此造成的损失便达17000多亿元”(张宏良，《乌有之乡》2007年6月5日)。二是今年2月27日美国财政部披露，到2008年6月底，中国所持有的美国“垃圾股票”、“垃圾债券”达到12050亿美元。加上美元贬值等等，致使“全国每人被剥夺一万多元血汗钱”<br />
<br />
(仲大军，《毛泽东旗帜网》，2008年11月)。</div>

							
							
							
							
