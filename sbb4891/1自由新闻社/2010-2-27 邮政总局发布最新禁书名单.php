由于国共两党垄断了历史的书写权，中国的近代史书充斥着重重谎言。本书试图破解自晚清直至中共建国前的一系列重大疑案，从官方版本出发，比较辨析各方证词，通过严谨的逻辑推导，推翻明显荒谬的说法，使用排除法导出可能性更高的结论，由此逼近事件真相。<br />
