5月20日影帝总理在捷克首都布拉格出席了第十一次中欧领导人会晤，并在会晤后与欧盟轮值主席国捷克总统克劳斯和欧盟委员会主席巴罗佐共同会见了记者。在会见记者时说：“今天早上我坐了十个小时的飞机如期与会。由于国内还有许多紧迫的事情要做，晚上就飞回北京，还需要十个小时，一共要在路上花二十个小时。”<br />
<br />
&nbsp; &nbsp; 事实正如总理所说的那样，我们国内还有许多事情要做，譬如，抗甲型H1N1流感的任务还很重，抗国际金融危机的任务还很重，总理身在国外仍然惦记国内还有许多紧迫的事情要做，晚上就飞回北京，让人感慨万千，倍感温暖，给人诸多启示，是一堂生动课。<br />
<br />
&nbsp; &nbsp; 首先，是一堂“统筹兼顾”课，总理在飞机上颠簸20个小时，早出晚归，他以最短的时间办成了一件大事，加强了中欧交流和合作关系，为中国的和平发展创造和谐的外部环境，但他惦记国内的事情，这给人一种启示，那就是要统筹好国内国际两个大局，欧洲不去不行，国内的事情不办不行，在当前全国抗甲型H1N1流感的任务还很重，抗金融危机的任务还很重的时候，我以为我们要善于把握经济社会发展全局，处理好各方面的重大关系，真正应用好科学发展观“统筹兼顾”的工作方法，我们各地政府在抗金融危机之际，不但要拓宽国内市场还需要拓宽国际市场，抗甲型H1N1流感之时，不但要留意国际情况还需要留意国内情况，严阵以待，同时，不但要关心本地区的发展还要关心兄弟省、市、县的发展，总之，只有加强信息共享、资源整合，人才交流，技术合作、互惠互利，密切联系，做到同心同德，我们一起努力就能战胜各种危机，完成“三保”任务。<br />
<br />
&nbsp; &nbsp; 其次，是一堂“牢记使命”课，温总理长途颠簸，按正常说，他完全有理由在捷克做短暂休整才回国，但他没有，一句“国内还有许多紧迫的事情要做”就完全道出总理的人民情怀，体现出总理的公仆意识、强烈的历史使命感和责任感，总理此举无疑给我们另一种启示，无论身在何地，不能乐不思蜀，都要用灵魂守护国家，用爱报答国家，要牢记自己身上肩负着党和人民的重托，当前，我们处于发展机遇期、转型期，加之现阶段面临的任务很重，我们就必须强化为人民服务的宗旨，加强党性修养，树立优良作风，时刻牢记使命，该以时不我待的奋发精神掌握发展先机，切实做到想人民之所想，办人民想办之事，把社会推向和谐，把我们国家建设的更美好，不辜负人民群众的重托。<br />
<br />
&nbsp; &nbsp; 再次，是一堂“厉行节约、讲究效率”课，总理出访不过夜已经不是首次，去年 12月中旬他赴日外访也是不过夜，总理外访不过夜，既办成事，又减少开支，他厉行节约无疑给我们又一启示，现在某些地方干部挂羊头卖狗肉，名为考察，考察团一到国外变成旅游团、购物团，这些干部在总理面前应该感到羞愧。我以为，人民群众不是反对考察，到国内交流、引资、洽谈有益我们的发展，人民不会反对，人民反对的是不厉行节约，不讲效率，我们需要衡量考察的意义，我们需要在时间、人数、行程和费用、管理、监督上做文章，该缩减要缩减，花最短的时间、花最少的钱办最好的事，特别是当前受国际金融危机的影响，中央增加巨额投资扩大内需，各省市宣布投资计划，我以为地方政府干部不管是国际考察还是国内交流，都必须开源节流，用好每一分钱，用钱要经得起人民的考验，厉行节约是我们党的优良传统，更是干部面对金融危机与人民群众共渡难关、共克时艰的具体表现。（文海）<br />
