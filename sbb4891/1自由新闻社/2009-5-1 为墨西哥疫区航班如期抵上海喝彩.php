为墨西哥疫区航班如期抵上海喝彩<br />
2009年5月1日 09:45<br />
选稿:马韵筠&nbsp;&nbsp;来源:东方网&nbsp;&nbsp;作者:亦菲&nbsp;&nbsp;<br />
<br />
　　震惊世界的猪流感爆发以来，公众都把目光盯住最先传出疫情的墨西哥与美国，关注墨西哥与美国的一举一动。媒体报道，4月30日早上6时，墨西哥航空公司AM098航班如期抵达上海浦东机场（4月30日《新闻晨报》）。<br />
<br />
　　此消息告诉人们，尽管墨西哥已是世界公认的此次猪流感疫情源头，疫情至今仍未得到遏止，然而，中墨两国航空依然如常双飞。<br />
<br />
　　在发生疫情的情况下，民航如期飞行是需要胆识与智慧的。人们看到，自从墨西哥冒出猪流感以来，某些媒体采取无限渲染对疫情胡编乱造的态度，在公众中产生了相当负面的影响。在美墨等国，一些人们闻猪流感色变，有些人们甚至也“闻猪色变”，像躲艾滋病一样躲避；也有的国家组织对疫区表现了“另眼相看”，一改正常交往秩序，甚至出现对疫区采取变相抵制的态度，这既不能有效防控猪流感，还有可能引发新的事端。因此，对疫区是否善待，是否理性就成了一个很实在的问题。<br />
<br />
　　面对墨西哥的灾难，中国首先在世界奏响了同情支援的声音。在继4月 29日中国政府宣布向墨西哥提供500万美元的人道主义援助，其中400万美元物资援助之后，中国卫生部发言人同日在记者会上宣布，“战胜疫情是我们共同的目标。我们非常愿意为发生疫情的国家和地区提供力所能及的支持和帮助”。据中央电视台报道，中国民用航空局29日紧急批复中国国际货运航空有限公司一班北京至墨西哥城运送人道主义物资的特殊包机任务，货机预计今天下午起飞。不言而喻，墨西哥航空公司航班依时在上海浦东机场降落是与中国对拉美疫区的方针一脉相承的。<br />
<br />
　　诚然，如期为墨西哥航班在中国放行并非放松必要的检查。相反，中国有关方面做出了一系列严格的检查规定。比如，来自墨西哥疫区飞机抵上海不停靠在廊桥，而是停在一个远机位，尽量与其他航班适当隔离；在登机之前，对旅客进行第一次测温，一旦发生异常，将取消有关旅客此次航程，机上并配备口罩、酒精；旅客下机后，进行第二次测温。此外，上海市卫生部门还安排了设施完备、消毒完好的救护车派遣到机场等候。而上海市长韩正等官员则在第一时间赴机场进行检查等等（4月30日《东方早报》）。应该说，上海航空部门对来自疫区的飞机进行严格检查也是可以理解的。这样，在世界面前，中国就展现了既保持两国航空渠道畅通，又从实际出发强化管理的实事求是态度，与那些闻疫色变，闻疫把正常秩序一切推倒的做法区别开来。<br />
<br />
　　笔者为墨西哥疫区的航班如期抵上海喝彩，是为中国坚持人道主义的精神喝彩。对于人道主义，中国不但一向旗帜鲜明地弘扬，即使在特殊环境中，中国也依然一以贯之般坚持。在弘扬人道主义的背后，闪烁着与人为本的光辉。有了这种精神，中国永远是世界各国人民的真诚朋友，中国的朋友遍天下。<br />
<br />
　　笔者为墨西哥航班如期抵达上海喝彩，是为中国民航等部门的实事求是精神喝彩。实事求是是中国执政党的优良传统，也是坚定不移的执政理念。不但在经济工作中要实事求是，在对外交往中处理国际矛盾也要实事求是。尽管猪流感是一个新的病毒，需要通过科学研究，找出其来龙去脉，更要用辨证的思维指导防控，中国提出联防联控的八大措施就是证明。这样就可以避免猪流感进一步蔓延，也避免由此衍生出新的经济社会问题。</div>

							
							
							
							
