													<h2>原文作者其他优秀作品赏（第一卷）</h2>
						
						
																											<div id="postmessage_85698" class="t_msgfont">岂容朱学勤向开国领袖毛泽东泼脏水<br />
<br />
再过几天就是12月26日――毛泽东同志诞辰113周年纪念日。近些天来，笔者点击主流网站，纪念毛泽东的言论照例源源而至。<br />
<br />
然而，与此不那么协调的是，就在国人纪念毛泽东诞辰113周年前夕，以历史学者朱学勤在媒体发表题为《鲁迅的思想短板》的长篇政论，在批评鲁迅的同时，再度把开国领袖毛泽东也捆绑在一起进行批评。朱学勤写道：尽管鲁迅与毛泽东一样忽视经济自由，尤其是制度层面经济自由，但是鲁迅竟与他有一点不一样：毛个性张扬，支撑他的是天子特权。（见《国际在线》 2006年12月19日）<br />
<br />
污蔑毛泽东享有“天子特权”是这位历史学者一大“发明”。这个论调不但中国人民通不过，连外国许许多多的政治家、国际友人也通不过。事实上，自从1976年9月毛泽东逝世以来30年间，尽管历经沧海桑田，但是对毛泽东的正面评价却从不止息，而且越来越深刻。比如，美国前总统福特称：毛泽东著作给人类留下深刻的印证，他确实是我们时代的一位杰出人物。法国前总统德斯坦说，由于毛泽东的逝世，人类思想的一座灯塔熄灭了。<br />
<br />
我国对毛泽东的正确评价集中于1981年十一届六中全会通过《关于建国以来党的若干历史问题的决议》，在坚持和维护毛泽东思想问题上，邓小平和历代中央领导集体一以贯之旗帜鲜明。邓小平指出，不仅今天，而且今后，我们都要高举毛泽东思想旗帜。又说，对毛泽东同志的评价，对毛泽东思想的阐述，不是仅仅涉及毛泽东同志个人的问题，这同我们党、我们国家的整个历史是分不开的。（见《邓小平文选》第二卷299页）<br />
<br />
朱学勤给毛泽东思想扣上“天子特权”实在是张冠李戴，驴唇不对马嘴。国人缅怀毛泽东，不仅怀念毛泽东率领人民推翻三座大山，创建人民共和国的历史功绩，同样怀念他不谋私利全心全意为人民服务的高贵品质。新中国成立后，身为国家元首的毛泽东，在亲朋戚友求官求得到好处面前，表现了深明大义和率先垂范，从没有特批一个，而是通过书信等渠道要求亲朋们尊重当地政府，靠自己的劳动本事吃饭。比如，1950年5月12日，毛泽东致信他的表兄文南松云：运昌兄（毛泽东的表兄）的工作，不应由我推荐，宜由他自己在人民中有所表现，取得信任，便有机会参加工作。（见《毛泽东书信选集》第339页）对于亲戚要求地方政府额外关照一事，毛泽东在信中同样明确表示反对。比如，1950年5月27日毛泽东在致信湖南湘乡县县长刘亚南时郑重表示，至于文家（我的舅家）生活困难要求救济一节，只能从减租和土改中照一般农民那样去解决，不能给以特殊救济，以免引起一般人民不满。（见《毛泽书信选集》第346页）像这类给亲戚朋友求官求特殊关照亮红灯的事比比皆是，已在人民中成为美谈。人们看到的是，毛泽东率先垂范作风也感染着他的家人。其中。毛泽东的长子毛岸英在赴朝参加抗美援朝之际，曾经有一次回到老家韶山，当时的亲戚也给他写了一封信，想讨个官做做。毛岸英给他的回信中有一句话至今读来仍振聋发聩的话：当皇亲国戚升官发财的思想是非常不合时宜的，我们伟大的新中国是靠着劳动创造财富的时代，不是靠升官发财的时代。<br />
<br />
可见，毛泽东一生中，非但没有半点“天子特权 ”，而是与人民永远同甘苦共患难休戚与共的人民领袖。朱学勤给毛泽东扣上“天子特权”帽子，列出所谓证据是毛泽东生前有200万稿费，认为这在消灭私产年代，几乎是中国最大的一笔私有财产。但恰恰相反的是，对这笔稿费，毛泽东也从不私用，而是交由中办“特别会计室”处理，即使是江青伸手也难以如愿以偿。由此看到，朱学勤称毛泽东是“天子特权”其实是给毛泽东泼出的一盆污水。对这盆污水，理所当然要拿到阳光下曝光，理所当然要抵制。<br />
<br />
在纪念毛泽东诞辰113周年之际，重温毛泽东全心全意为人民服务的品格，剖析朱学勤“天子特权”怪论，人们似乎又多了一层思考。(亦菲)<br />
