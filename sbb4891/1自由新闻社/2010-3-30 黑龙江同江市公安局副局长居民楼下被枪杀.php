													<h2>黑龙江同江公安副局长疑被前政委指使凶手杀害</h2>
						
						
																											<div id="postmessage_552592" class="t_msgfont">新京报4月7日报道 黑龙江同江市公安局副局长孙巍于3月28日被枪杀身亡。<br />
<br />
关于孙巍被杀，存在两种说法。佳木斯警方宣布称，刘祥军因“个人恩怨”行凶。而当地传言，此事为同江市公安局前政委陈晶指使刘祥军所为。<br />
<br />
记者从孙巍和刘祥军的家属和朋友处获知，两人均未提及过对方名字和有何恩怨。而同江市公安局警员表示，刘祥军和陈晶交好；陈晶曾因孙巍的检举而被免职。<br />
<br />
有消息称，目前陈晶已被带走调查。但记者未能从佳木斯警方获得证实。<br />
<br />
3月28日，黑龙江同江市公安局副局长孙巍被枪杀于自家楼下的警车内。<br />
<br />
4天后，佳木斯市公安局宣布，犯罪嫌疑人刘祥军被抓获，犯罪动机是“个人恩怨”。<br />
<br />
在当地，盛传着另一说法，同江市公安局前政委陈晶参与凶杀案。同江公安局一名老干警――曾是陈晶的下属，对记者说，案发后陈晶便被带走调查。<br />
<br />
记者未能从佳木斯警方核实到陈晶是否被带走，以及陈是否与孙巍被杀案有关。<br />
<br />
地处中俄边境的这一起凶杀案，显得迷雾重重。<br />
<br />
因“个人恩怨”杀人？<br />
<br />
警方称，刘祥军因“个人恩怨”杀害孙巍；而传言称刘系受雇杀人<br />
<br />
3月28日18点半，同江市天已黑。虽是周日，4级的西风令主干道上见不到多少车辆和行人。<br />
<br />
农业开发银行家属楼一楼门面房内，黄老板开了美术班，他从窗口向家属区望去，能看见孙巍的警车，牌号为黑D0032。<br />
<br />
19点40前后，黄老板听见救护车的鸣笛声。笛声停至家属院。随后多辆警车接踵而至。直到警察来问询，黄老板才得知发生凶杀案。被害人是住在家属楼上的孙巍。<br />
<br />
黄老板和一楼七八户门面房租户均表示，未听到枪声。<br />
<br />
孙巍被送往医院后，医生确认其已经死亡。经法医检验，孙巍系被猎枪击中头部致死。<br />
<br />
佳木斯公安局随即下派30多名警察赴同江调查。<br />
<br />
4月1日，佳木斯警方宣布，犯罪嫌疑人刘祥军于3月29日被抓获。刘交代3月28日晚，吸食冰毒后，向朋友李某借来无牌照帕萨特轿车，携带私藏猎枪，来找孙巍。<br />
<br />
当晚18时30分，刘祥军看到孙巍的车停在楼下，便在小区等候。19时零5分，孙巍刚进入汽车，刘祥军上前，向孙头部开了一枪，后逃离现场。<br />
<br />
一位接近调查组的警方人士说，刘祥军是在同江市一处住宅被抓，一同缴获的还有部分毒品。目前，刘祥军涉嫌故意杀人罪已被刑拘。<br />
<br />
佳木斯警方还宣布说，刘祥军的犯罪动机是与孙巍有个人恩怨，怀恨在心，故产生了除掉孙巍的念头。<br />
<br />
而在佳木斯警方宣布破案之前，当地还盛传另一种说法，刘是受人指使杀人。<br />
<br />
被害者不识“凶手”？<br />
<br />
孙巍家属、同事表示，未听孙提过刘的名字，更不知两人有何恩怨<br />
<br />
对于孙巍和刘祥军的恩怨，孙巍的哥哥向记者表示，“孙巍不容易和人结仇，家人也从未听他提及凶手名字。”<br />
<br />
孙巍的同事们也大都表示不知道犯罪嫌疑人刘祥军。同事们对孙巍的评价是，“脾气很好”，“在同事中的人缘不错”。<br />
<br />
同江公安局一位与孙巍关系不错的同事说，孙巍是转业军人，从政工科科员干起，后升任科长、办公室主任、公安局副局长。被害前，孙巍已担任十几年副局长。<br />
<br />
“孙巍会和领导相处，领导对他很满意。”这位刑警说。<br />
<br />
王华光曾是孙巍的上级领导。他说，孙巍是同江本地人，“家境一般，没有买车，一直开单位的警车。”<br />
<br />
王华光对孙巍的印象是“温和”、“和蔼”，“以前和孙巍一起出差，他坚持把舒适的床让给我，自己睡硬板床。”<br />
<br />
王华光告诉记者，“若能因个人恩怨杀人，那这恩怨一定小不了。但从未听孙巍说过刘祥军”。<br />
<br />
同江公安局一位与孙巍相熟的警员分析，据他所知，刘祥军和孙巍的生活交集要出现应该是出现在1986年，刘祥军因盗窃被劳动教养3年。案子可能由同江公安处理，这是他能想到的孙刘发生矛盾的原因。此外也有可能是别的哪一处前科劣迹。<br />
<br />
“我没听说他们有矛盾。即便有矛盾，那也是20多年前的事了。”这名警员说。<br />
<br />
记者就此向同江市公安局副政委王兵询问此事。对方拒绝回应。<br />
<br />
“凶手”曾和前政委交好？<br />
<br />
刘祥军亲属和朋友表示，未听刘提过孙的名字，但刘和同江公安局前政委陈晶交好<br />
<br />
在采访刘祥军亲属时，他们也说，未听刘提及过孙巍的名字。<br />
<br />
刘祥军来自勤得利农场。这个距同江市63公里的农场，是上世纪50年代末由铁道兵开垦出来的。当地人称刘祥军为“刘四”。<br />
<br />
刘四排行老四，三个兄姊都是公职人员，父母是老农垦工人。据刘四的一名小学同学回忆，刘四从小是个“二愣子”，喜欢以打架来解决纠纷。<br />
<br />
上世纪90年代中，刘四跑客运，从勤得利农场到前进农场。“那时挺憨厚的。”在前进农场，他认识了当地有名的黑社会头子，走上歪路。<br />
<br />
2000年前后，刘四开沙厂和石场。<br />
<br />
附近龙老板的石场因价格便宜而生意兴隆。刘四便雇人把龙家石场打砸一番，并将龙老板的妹妹打成重伤。<br />
<br />
“每次报警，人就跑了，没有下文。”龙老板说。<br />
<br />
该同学说，因为刘四有“官方背景”，刘的妻妹嫁给当地公安分局的一名警察王力。<br />
<br />
后来，当地几家采石场先后濒于破产。<br />
<br />
很多居民反映，2005年以后，刘四变得蛮横，一语不合就骂人打人。刘四的作风还令同江公安局法制大队一名警员印象深刻。他说，当时他们曾接触过刘四，刘在电话里对其破口大骂。<br />
<br />
龙老板告诉记者，刘四常炫耀自己在公安局后台很硬。届时，他的连襟王力已是勤得利公安分局的副局长，并多次提到自己和佳木斯以及同江公安局的领导关系很好，其中还有同江公安局的前政委陈晶。<br />
<br />
房产开发商于光景和刘祥军交往颇多并有合作项目。他说，自己从未听刘四说过孙巍。<br />
<br />
刘爱臣，刘祥军的儿子，他接受采访时只是反复地说，他并不知道父亲犯罪的动机，也没有听说过孙巍这个人。刘家一位亲戚对记者强调，“我们都没听说过孙巍这个人。”<br />
<br />
当地很多居民表示，刘四虽然猖狂，但大家都想不到他会去杀警察。<br />
<br />
前政委卷入凶杀案？<br />
<br />
有警员反映孙和前政委陈晶不和，因孙检举，陈被免职<br />
<br />
在同江公安局采访中，局里一名警员――陈晶的老下属，告诉记者，陈晶与刘四相识，并曾帮过刘四。<br />
<br />
这位老刑警说，2000年前后刘四曾为个案子找陈晶帮忙，陈晶答应了。当时这名刑警一直负责该案，因此知道陈和刘之间的关系。“此后，陈晶对刘四也多有照顾。”<br />
<br />
这名警员还说，在局里，陈晶与孙巍的矛盾已不是秘密。<br />
<br />
陈晶，同江市公安局前政委，曾任抚远县公安局副局长，同江市公安局副局长，国际刑警。<br />
<br />
一名抚远警员告诉记者，陈晶在抚远县公安局时颇有名气。<br />
<br />
上世纪90年代中期，陈晶调至同江市公安局任副局长，主抓刑侦。陈晶同事对他的评价是，“能力非常强，破案有一手。”<br />
<br />
据同江市公安局一名与陈晶相熟的警员介绍，陈晶深受市里领导赏识，但很长一段时间内没当上政委，是因为他交往人员复杂，此外还有传言说他吸毒。因此曾在政委换人时因被举报而未能当上政委。<br />
<br />
2006年夏天，陈晶被任命为同江市公安局政委。<br />
<br />
在任政委前的几年中，陈晶抓经侦、缉毒和户政。孙巍抓刑侦和治安。有警员反映，在那段时期，陈晶和孙巍两个人的矛盾开始显现。<br />
<br />
同江市公安局有警员说，公安局副局长的一个朋友曾要被抓到局里来调查，副局长向陈晶求情，陈晶下令别抓。而几天后陈晶出差，孙巍将人抓了起来。“这件事令陈晶很尴尬。”<br />
<br />
这名警员说，陈晶和孙巍，两人性格完全不同，一急一缓。在业务上，两人也常暗中较劲。<br />
<br />
调查中，记者了解到，在2009年，孙巍本有可能被调至抚远县任公安局政委。孙巍将消息告诉刑警大队的同事，表示走之前要请客吃饭。这一消息随即为公安局内部很多人得知。<br />
<br />
孙巍的一名同事说，但出于各种原因，孙巍没能升任。“孙巍担任副局长十多年才得到这么一个机会。他今年五十，若再不提，就没机会了。”<br />
<br />
一名与孙巍关系很熟的警员说，他没想到，孙巍会在今年春节前，向上级领导检举，陈晶与公安局同事赌钱，且赌资巨大。<br />
<br />
上级调查后免去陈晶政委一职。此时陈晶53岁，没有到退的年龄。<br />
<br />
这名警员说，“春节后，听说孙巍被叫去谈话，将被提拔为新政委。”<br />
<br />
3月28日，孙巍被杀。<br />
<br />
谜团待解<br />
<br />
记者了解到，目前并无证据证明陈晶涉案<br />
<br />
同江公安局多名警员告诉记者，陈晶案发后不久被带走调查。<br />
<br />
记者一一拨打了同江市公安局领导的电话号码，只有陈晶和孙巍两人的手机关机。<br />
<br />
在局里，有警员认为，即便陈晶因孙巍而被免职，但也不至于做出那样的“傻事”。<br />
<br />
案发后，还有传言称，警方抓到陈晶、刘四和李某一起吸食冰毒。<br />
<br />
这名干警否定这一说法。他透露，确实从陈晶家搜出毒品，但这房子是陈晶用来招待朋友，不能确定毒品为陈晶所藏。此外陈晶在案发后第一时间和领导一起在局里待命，配合佳木斯公安局调查组调查案情，不可能与刘四在一起。<br />
<br />
在调查中，记者得知，目前并没有直接证据指向陈晶参与凶杀案。<br />
<br />
4月1日，记者从同江市委宣传部副部长张浩处得知，局里领导架构已做出调整，原局长郭吉龙离任，新局长和政委均已到任并公示。在同江市公安局，工作人员已将告示牌上孙巍值班的信息更换掉。<br />
<br />
当天，佳木斯市公安局在通报中称，此案正在进一步审理中。<br />
