<br />
十多人轰赶“穿和服照相”母女。<br />
<br />
本报讯 (长江商报记者 汪志 通讯员 闫西子 张雨舟 实习生 吴丹平)“不要穿和服在武大拍照！”“穿和服的日本人滚出去！”昨日下午3时左右，一对母女在武大樱园内穿和服拍照，引来众多学子围观声讨。<br />
<br />
两女子樱园内穿和服拍照<br />
<br />
下午3时左右，两个穿日本和服的女子出现在樱花大道上，年纪较长的穿了件淡紫色和服，另一年轻女孩穿一件彩色和服，两人站在樱花树下合影留念。给她们拍照的是一位中年男子，随行的还有一位年轻女子。4人都操武汉口音，在樱园逗留了近10分钟，引来很多赏花市民的目光。<br />
<br />
十多人自发轰赶“和服母女”<br />
<br />
“不要穿和服在武大拍照！”“穿和服的日本人滚出去！”突然，一位穿蓝条纹衣服的小伙子冲他们吼起来，接着一名年轻女子也加入声讨行列。吼声吸引了10米开外游人的注意。两女子一下被镇住了，马上逃到另一处。随后，十多名年轻人加入声讨队伍，很多围观市民也表示愤慨，责备声越来越大。<br />
<br />
眼见情况不妙，母女俩没有反驳，立即停止拍照，脱下和服交给随行的年轻女子，收拾东西匆匆离开。记者看到，母亲脸涨得通红，年轻女子安慰刚脱下和服的年轻女孩说：“不要理他们，没什么的，一群神经病。”<br />
<br />
“只是觉得穿和服拍照好看”<br />
<br />
记者上前询问得知两人为母女。问她们为什么穿和服来樱园，这对不愿意透露姓名的母女表示：“穿和服拍照只是觉得好看，没考虑其他的，没有任何意图。”<br />
<br />
记者随后寻找那位率先声讨的小伙子，但他已离开。据了解，此人是武汉大学信息学部大二学生。另一位参与声讨的年轻女子表示：“虽然和服有美丽之处，但它毕竟是日本人特有的服饰，让人看了很不舒服，有种异样的感觉。”此外，也有学生认为，这对母女的行为属个人行为，其他人不该干涉。</div>

							
							
							
							
