<br />
眼见情况不妙，母女俩没有反驳，立即停止拍照，脱下和服交给随行的年轻女子，收拾东西匆匆离开。记者看到，母亲脸涨得通红，年轻女子安慰刚脱下和服的年轻女孩说：“不要理他们，没什么的，一群神经病。”<br />
<br />
“只是觉得穿和服拍照好看”<br />
<br />
记者上前询问得知两人为母女。问她们为什么穿和服来樱园，这对不愿意透露姓名的母女表示：“穿和服拍照只是觉得好看，没考虑其他的，没有任何意图。”<br />
<br />
记者随后寻找那位率先声讨的小伙子，但他已离开。据了解，此人是武汉大学信息学部大二学生。另一位参与声讨的年轻女子表示：“虽然和服有美丽之处，但它毕竟是日本人特有的服饰，让人看了很不舒服，有种异样的感觉。”此外，也有学生认为，这对母女的行为属个人行为，其他人不该干涉。 长江商报 (本文来源：浙江在线 )</div>

							
							
							
							
