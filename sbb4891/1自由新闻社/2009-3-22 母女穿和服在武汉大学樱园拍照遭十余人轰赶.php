<br />
两女子樱园内穿和服拍照<br />
<br />
下午3时左右，两个穿日本和服的女子出现在樱花大道上，年纪较长的穿了件淡紫色和服，另一年轻女孩穿一件彩色和服，两人站在樱花树下合影留念。给她们拍照的是一位中年男子，随行的还有一位年轻女子。4人都操武汉口音，在樱园逗留了近10分钟，引来很多赏花市民的目光。<br />
<br />
十多人自发轰赶“和服母女”<br />
<br />
