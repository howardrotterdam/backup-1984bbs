金正银送车送楼激励入16强<br />
<br />
北韩领导处于换班之际，国家队在世界杯的成败，深具政治意义，如在世杯取佳绩，可视为送给被父亲金正日钦点为接班人的金正银「登基」的大礼。因此，金正银为激励士气，曾承诺只要打入16强，球员获送车送楼，可获封民族英雄，并加入劳动党。<br />
<br />
送车送楼　遣御用大厨<br />
<br />
北韩高层消息透露，金正银今年4月和5月曾亲自下令球员「要打造能攻能防的无敌之旅」，并让球员「火线入党」。「火线入党」原指战争时在战场入党，金正银这么说是指让球员毋须经复杂程序直接入党。<br />
<br />
他5月初又再前往国家队在平壤两角岛的集训场，向球员承诺：「如果在世杯取得好成绩，会给予相应的礼物。」北韩队原本没有营养师，为激励士气，他慷慨地派遣自己的营养师，即「御用大厨」，为球队进补。<br />
<br />
如此关怀备至，国家队输波，金正银自然怒气难消。<br />
<br />
南韩《朝鲜日报》</div>

							
							
							
							
