北韩44年首次晋身世杯决赛周，国内期望极高，国家队下榻酒店订到决赛日，一哭成名的前锋郑大世更以捧杯为目标。他们分组赛首场对五届冠军巴西，虽败不辱，仅负一比二，表现大受好评。北韩于是破天荒直播第二场对葡萄牙的赛事，希望可藉国家队拼搏表现，增加金正日威信，也提高他钦定接班、多番为球员打气的金正银声望。但事与愿为，北韩队这个回合大失水平，惨吞葡萄牙七蛋，创下赛事最悬殊比分。<br />
<br />
比赛后，有葡萄牙球员担心北韩球员回国后，会被送到煤矿，金正勋当时乐观地说：「我相信我国人民会张开双手来迎接我们的。」他并主动承担责任，称自己战术指挥的失误，招致空前惨败。但有报道踢爆，由于北韩直播比赛，关乎国家面子，因此金正日致电遥控赛事，要求球队压上抢攻，结果导致后防空虚，才被葡萄牙彻底击败。<br />
<br />
日本传媒指，金永勋负荆请罪，也不能平息金正日怒火，北韩足协主席文在哲成为最终的牺牲品，在世杯结束后便被辞退，由外贸部长李龙男接任。彭博社/《日本经济新闻》<br />
<br />
前教练：输波要去g石仔<br />
