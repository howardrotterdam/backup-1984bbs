世杯输波　北韩批斗国脚<br />
负金正银厚望　闹足6小时<br />
<br />
南非世界杯曲终人散半个月，输波的秋后算账才开始。一直有传输波就要回国当矿工的北韩国家队员，在世杯分组赛表现虎头蛇尾，回国后暂未被罚g石仔，但却被送往批斗大会。北韩官员指摘，国家队表现欠佳，有负最高领袖金正日幺子金正银的厚望，教练金正勋和球员在400多名官员和群众面前，接受六小时严厉批评。<br />
<br />
北韩输波出局，球员上月底打道回府途经北京时，还往烤肉馆和按摩店，神情轻松，但原来严苛的惩处，正等候着他们。自由亚洲电台引述消息报道，北韩本月2日在平壤人民文化宫召开思想斗争会，批斗对象是出战世杯的国家队球员，但不包括两名在日本出世的「朝鲜朗尼」郑大世和效力日职大宫松鼠的安英学。<br />
<br />
不包括郑大世　安英学<br />
<br />
消息透露，批斗大会不只一场，平壤日前还对球员进行过「大论争聚会」，出席者包括北韩体育相朴明哲和劳动党官员、体育省属下各体育项目选手、平壤体育大学和金日成综合大学学生等，400多人以非公开的形式，对球队进行了批斗。<br />
<br />
大会上，在北韩对葡萄牙赛事直播担任嘉宾评述的朝鲜体育大学教授李东圭率先发炮，批评每名国脚的漏洞，之后其它与会者加入批斗行列。首当其冲是教练金正勋，他先遭到体育省和大学体育选手批评，随后球员轮流上台尖锐批评这位带领他们出战世杯的教练。批斗大会总结，国家队「辜负了青年将军金正银的信任」，有人要为此承担责任。<br />
<br />
金正日遥控球队致大败<br />
