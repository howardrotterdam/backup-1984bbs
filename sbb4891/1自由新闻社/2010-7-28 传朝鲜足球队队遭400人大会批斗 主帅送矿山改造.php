<br />
伊拉克在侯赛因独裁统治下更苦。侯赛因长子乌代担任伊拉克奥运委员会主席近20年间，为逼出球员好表现，常常出动手指夹板和极刑面具，以大刑伺候他们。球员出赛射失十二码或表现差，轻则剃头掌掴，重则被送进军事监狱、背部被电线鞭打到爆血、在54°C高温下踢「石屎」球、连做12小时掌上压和全速疾跑等。有球员和乌代理论，险被勾去筋。《苹果》资料室</div>

							
							
							
							
