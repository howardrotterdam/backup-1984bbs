																											<div id="postmessage_112754" class="t_msgfont">“两岸猿声啼不住，轻舟已过万重山”竟然说是苏东坡的诗，上来就犯低级错误。<br />
<br />
十五年前，我是一个怀孕的女人，在不可预知的机缘里，走了三个广场：北京的TAM广场、东柏林的亚历山大广场、莫斯科的红广场。那是动荡的一九八/九年。<br />
为了纪念「五四」运动七十周年，我来到北京。清晨时刻，雾，还锁着昏昏的建筑，覆着疲惫的人群，广场在朦胧中却显得深不可测，像秘密无声的山谷。<br />
但是你知道山谷不是空的，一波一波的回声涌动，推着历史的隆重自转。一八九五年甲午战败后的呼喊，在一九一九年一战之后得到呼应；一九一九年的呼喊，「要民主，要科学，要国家富强」，在一九四九年得到庄严的呼应：「中国人民从此站立起来了！」对着一九四九年的庄严誓词，一九八九年发出呼喊── <br />
没有人想到，回应誓词的是屠sha的gun声、坦k的震动，和长达十五年的灭音。<br />
可是亚历山大广场上人潮汹涌，上百万的东德人每天上街，高举着拳头，要求开放边境，要求民/主/自/由。突然之间TAM的枪响传来，德国人走在街上，脸上有血色的愤怒，但是心里有白色的恐惧：TAM的屠杀，是否也会在东柏林发生？<br />
我到了柏林城外，想感觉一下乡村的情绪。中午的太阳辣辣地照着，小村广场上只有一只老狗趴着打盹，看起来安详静谧。但是在广场地面上，有人用粉笔画了什么，白白的一片。我走近去看，画的是一个中枪倒地的人形，四肢呈「大」字打开，中间用德文清楚写着：「TAM，六月/四日」。<br />
又过了几个月，我在莫斯科的街头。成千上万的人，孩子骑在父亲的肩上，母亲推着婴儿车，白发苍苍的老年人手挽着手，大声呼喊：「自由！自由！自由！」白色的布条横过整条马路，用各种文字写着：「我们不要TAM！」每一条横巷内都藏着军用卡车，卡车里塞满了全副武装的士兵，紧抱着枪，全神戒备。<br />
我怀孕的那一年，柏林围墙被人民推倒；苏联帝国轰然解体。事后，我们知道，当呼啸的人民像洪水一样自街头流过，这些党的领导人躲在高楼的办公室里激烈地辩论是否也采用「TAM模式」来保住政权。但是TAM的屠杀太过残酷，给世界的震撼太过剧烈，被过于巨大的罪行所震慑，两个城市的领导人，在最紧迫的时刻，按住了枪口。<br />
柏林围墙崩溃前夕，东德领导阶层乱了手脚，譬如说，对试图越墙逃跑的人民，是否还是一律「格杀」？一个高阶领导后来回忆说，「当时，我就给自己立了一个分清是非的标准：TAM发生屠杀时，你是站在哪一边？站在人民这一边的，就是对的。这么一想，我就知道该怎么办了。」<br />
北京的TAM，成为动荡中的东欧用来判别是非的准则、分辨真假的测谎器。<br />
是的，你可以说，中国的血染大地成就了东欧不流血的革命。 <br />
&nbsp; &nbsp; 2 <br />
十五年之后，在香港一个高贵的晚宴上，我遇见了这么一个姿态优雅的上海女性，从美国留学归来，在香港公司任经理，用英语说，「BANNED？不过是中国进步过程里打了一个饱嗝罢了！」 <br />
&nbsp; &nbsp; 中国的「进步」，在她身上那么清楚地呈现：经济的起飞已经培养出一整代欣然自得于个人成就而对「BANNED」一无所知的人。或者并非一无所知，但在物质追逐的游戏中早已接受了一种逻辑，就是说，没有镇压，就没有今天的进步，镇压是进步的必然条件。对更年轻的一代而言，「BANNED」屠杀则根本不存在。历史的杀人灭迹，由国家执行起来特别专业、特别有效。<br />
中国在「进步」，像一个突然醒过来的巨人迈开大步在赶路，地面因他的脚步而震动。民间社会的自主空间逐渐拓宽，民权观念悄悄萌芽，经济的发展更是举世侧目。二○○八年的北京奥运、二○一○年的上海世博，还没有发生，但是仅仅是预期就已经使得许多中国人觉得光彩万分，心中满溢着强国盛世即将来临的自豪感。<br />
然而有多少人看见，巨人是带着一个极深的伤口在赶路的？ <br />
&nbsp; &nbsp; 「BANNED」的镇压，使得无数的中国精英流亡海外。诗人、作家、思想家、科学家、经济学者、未来的政治领袖人才……，这些中国最优秀的头脑、最细致的心灵，被迫留在异乡的土地上，幸运者成为别国的文化养分，不幸者提早凋零殒灭。<br />
没有一个真正富强的国家不把人才当做国宝的，或者应该倒过来说，不把人才当做国宝的国家，不可能真正富强。回首五十年，一整代菁英被「反右」所吞噬，又一整代被「文革」所折断；「BANNED」，又清除掉一代。五十年共产党的历史简直就像一只巨大的筛子，一次一次把国家最珍贵的宝藏筛掉。一路抛弃宝藏，巨人你奔往哪里？<br />
或者说，「BANNED」被放逐的是少数，而且中国大，人才无数，反正筛掉了又有新的一代冒起。<br />
&nbsp; &nbsp; 再多的麦子若是掉在石砾里，也是要乾枯的，所以麦子多寡不是问题，土地的丰润与否才是。只有当国家以制度来保护「独立之精神、自由之思想」时，人才才可能像麦子落土悠然茁长，然而只要镇压「BANNED」的道德逻辑还在──这个逻辑将对于党的忠诚凌驾一切，将粗暴的权力视为当然──那个制度就不存在，人才也无从焕发；集权的逻辑是一把锁，锁住整个社会结构，让自由的心灵、爆发的创造力、无边的想像力处于不能动弹的地位。<br />
高楼越来越多，道路塞满了汽车，商场人头钻动，飞弹战机精良耀眼，奥运世博国威赫赫，这些或许都是值得自豪的成就，但是有两个问题不能回避：第一、它是以什么代价换来的？那个代价可以不偿还吗？第二、它是可长可久的吗？没有「独立之精神、自由之思想」的保障，就不会有真正对弱势的照顾、对异议的容忍、对强权的反省、对法治的尊重、对人道的坚持、对正义的当仁不让，也不可能在文化艺术的创作上登峰造极……，缺少公平正义、缺少温柔力量、缺少自由精神的国威赫赫，难道是中国人真正的追求吗？<br />
「BANNED」屠杀，不是中国这个巨人打了一个饱嗝，而是巨人身上一个敞开溃烂的伤口。伤口一天不痊愈，巨人的健康就是虚假的，他所赶往的远大前程，不会真的远大。 <br />
&nbsp; &nbsp; 3 <br />
十五年过去了，谁看得见这个伤口？ <br />
&nbsp; &nbsp; 国际看得见。 <br />
&nbsp; &nbsp; 一九九四年，我还在海德堡大学汉学系任教。突然发现那一年的研究生数目骤减，几乎开不成课。我们很纳闷，几经推敲，找出了原因：九四年进研究所的，大致是一/九/八/九、九○年间进大学的人。TAM发生屠杀后，那一年汉学系几乎收不到学生。对中国的失望和厌弃，使得欧洲学生拒绝汉学。 <br />
&nbsp; &nbsp; 十五年来，欧洲人忘了「BANNED」吗？中国的市场，以及藉由市场所展现的国力「崛起」，赢得了国际的尊敬吗？中国的电视镜头跟着领导人出访，让人民看见，譬如说，法国总统铺排的红地毯礼遇，但是镜头删掉的，是法国文化界、知识界、民间团体对中国人权的抨击。各国政府纷纷来到中国竞争市场，但是尊敬？对不起，没有人会尊敬市场的；这个世界再怎么现实再怎么野蛮，最终赢得国际尊敬的，不是市场或武力，而仍是一个国家文明和道德的力量。今天美国失去好大一部分世人的尊敬，不是由于它的国力减弱，而是由于虐囚事件暴露之后它所丧失的道德立场。中国要得到泱泱大国应得的尊敬，不在于市场之大，国土之广，人口之多，而在于它道德担当的有无。<br />
「BANNED」使中国的道德破产。 <br />
&nbsp; &nbsp; 没有忘记这个伤口的，还有台湾人，还有香港人。 <br />
&nbsp; &nbsp; **的领导人一定问过自己：为什么用「血浓于水」的「民族大义」跟台湾人讲不通？为什么对香港释出了大量的利益，香港人仍旧若即若离？领导人愿不愿意面对这样的答案：台湾人抗拒，香港人挣扎，和「BANNED」的道德破产是紧密相关的。 <br />
&nbsp; &nbsp; 对于香港人而言，今天可以释出的利益，是明天可以收回的威胁。二十三条带来恐慌，难道和「BANNED」的血腥记忆无关？对于台湾人而言，听一个对自己人民开枪的政权大谈「民族大义」、「血浓于水」，除了恐惧和不信任之外，还可能有其他的感觉吗？ <br />
&nbsp; &nbsp; 「BANNED」屠杀代表权力的野蛮，理性的丧失，人性的沉沦，只要一天不平反，它就一天刻在北京政府的额头上。带着这样的「黥面」，你如何以文明的姿态去和台湾人或香港人谈「统一」、谈「爱国」？简单地说，你，如何让人相信？这个沉重包袱，对于力求改革的新领导人或许不公平，但是政治责任本来就是「概括承受」的，不是吗？ <br />
&nbsp; &nbsp; 如果有人以为「BANNED」仅只是那一小撮流亡海外「不成气候」的民运分子的事，关系不大，那就真看错了。「BANNED」平反不平反是一个良心的测谎器、道德的试金石，更是两岸政治和解路上一块怵目的绊脚石。北京政府如何对待「BANNED」，意味着它是走向民主自由还是继续极权统治，也关键地影响台湾人对中国的态度。马英九在两年前纪念「BANNED」的文章中有一句话：「『BANNED事件』必须平反，这必将是大陆民主化与两岸政治统合成败的重要指标。」对于许多台湾人来说，两岸的对峙，民进党不是问题所在，台独不是问题所在，真正核心的症结──北京领导人不可能不清楚──是中国本身的民主化进程，而「BANNED」，是一个人们每天看着、无时暂忘的指标啊。 <br />
&nbsp; &nbsp; 遮掩伤口所引起的最后的全身败坏，我们是目睹过的。二二八的流血事件被国民党遮盖了四十年。四十年中，家破人亡的痛苦无处申诉，流亡海外的委屈无法纾解，仇恨因为掩藏而更加深化；四十年后，国民党固然因而失去了政权，人民也被一种积累的苦大仇深所撕裂、所折磨。<br />
「BANNED」敞开的伤口已经被掩盖了十五年；是抢时间尽快把盖子打开，让它在温柔中愈合？还是继续掩盖，让它在缄默中溃烂？ <br />
&nbsp; &nbsp; 4 <br />
今天，二00四年六月四日，晚上八点，我会去维多利亚花园点亮一盏蜡烛，追思「BANNED」的亡魂，带着我十五岁的孩子。在我胎中时，他曾经陪我走过三个广场，看人们用肺腑的力量在呼喊，不同的语言──德语、俄语、汉语，却发出一样的声音：「民/主/自/由！」而如果孩子说，「母亲，我有自由啊，『BANNED』和我没什么关系」，我想我会这样告诉他： <br />
&nbsp; &nbsp; 孩子，你是否想过，你今天有自由和幸福，是因为在你之前，有人抗议过、奋斗过、争取过、牺牲过。如果你觉得别人的不幸与你无关，那么有一天不幸发生在你身上时，也没有人会在意。我相信，唯一安全的社会，是一个人人都愿意承担的社会，否则，我们都会在危险中、恐惧中苟活。 <br />
