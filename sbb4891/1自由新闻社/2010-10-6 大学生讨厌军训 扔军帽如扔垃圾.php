大学生讨厌军训　扔军帽如扔垃圾<br />
<br />
<img src="http://thumbsnap.com/i/lS5dPpCy.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
学生完成军训后，将军帽扔上天空。<br />
<br />
「军训结束了，难道浪费（就）开始了？」内地大学新生自 1989年六四事件后，开学的第一门课就是接受军训，但近年不少大学新鲜人都认为现时军训落伍，也质疑教官在军训期间不断虐待他们，让他们受尽苦头。终于捱到军训结束，湖北一间大学的学生将一肚怒气发泄在军帽上，向天一抛后便扬长而去，满地帽子变成垃圾的情景，引起网民热议。<br />
<br />
近日湖北武汉某大学举行军训总结大会，会场结束后，现场遗留上千顶红色、蓝色军帽，无人收拾，如同垃圾。网民拍下片段放上网，短短四日吸引 52万网民观看，并引起网民热议。有网民认为，学生的做法如同高考结束，学生将大量笔记从宿舍中扔下楼一样，如同发泄。也有网民认为一套 200元（人民币。下同）的军服，大学新生只穿一次，之后会以 8元卖给回收者，军帽、领带都不会被回收，基本上一开始这些配件便注定成为废物，实在浪费。<br />
<br />
<img src="http://thumbsnap.com/i/gi5L6JkV.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
现场留有大批军帽，无人清理。<br />
<br />
不满遭教官欺负<br />
<br />
自六四事件后，大学生入学后都被安排接受包括列队、体能训练、急救及军事理论等的军训，旨在加强「爱国主义教育」。但娇生惯养的 90后大学新生，大呼需要大量体力劳动的军训辛苦，甚至有学生批评被教官欺负。<br />
<br />
四川一间大学的教官，日前要求军训不合格的学生在操场列队，然后逐一起飞脚猛踢；南京一间大学的教官在检查学生收拾的床铺后，将不合格学生的被铺扔到被大雨淋湿的操场上，要学生冒雨拾回。 90后大学生更不满现时军训的内容，他们质疑要学生在烈日下步操的成效，建议不如修改内容，增加如在地震中如何逃生、自救方法，更为配合时代进步。<br />
<br />
湖北《楚天金报》</div>

							
							
							
							
