																											<div id="postmessage_722720" class="t_msgfont">来源：<a href="http://bbs.hoopchina.com/1396135.html" target="_blank">http://bbs.hoopchina.com/1396135.html</a><br />
<br />
看到楼下狼籍满地，到处是碎玻璃，破酒瓶。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;想到今晨将会有很多的保洁人员不辞辛苦地收拾这一场暴乱后剩下的烂摊子，想到现在可能还在医院躺着的那位被天降横祸砸成重伤，血流满地的送外卖者，心中多少有些不是滋味。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;仅代表个人向他们致歉。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;由于这几天持续的高温，晚上寝室闷热难耐，仅靠两个只能吹到下铺的不断电公共电扇根本不能让上铺的同学正常休息，于是学校在两天前贴出通知，表示在放假前不会再断电，这样上铺的同学就可以用自备的电扇，尽管还是休息不好，但总比没有任何降温措施要强很多。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;但是这种不断电的“好”日子随着7月四号11点整栋楼的电灯骤然熄灭而终止，学校事先没有给出任何改动的通知，这让刚刚享受到不断电“福利”的同学们很是恼火，我也一样，于是骚动开始蔓延，各个楼都充斥着咒骂和不满的吼声，我想这可能是学校在做通知下达工作时有疏漏，或者没把意思表达清楚导致我们误会了通知的真实含义，总之是一场误会。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;既然是校方和学生有误会，我想但凡是聪明一点儿的，负责任点儿的，有所谓“应急预案”的，学过所谓“危机公关”的，见识过大学生凝聚力的都应该把电及时的输送回各个寝室，这样也就不至于让无辜的人受重伤，让美丽的华理校园破败不堪。但让我气愤和觉得可悲的是，灯就这么一直灭着，人就这么一直热着，“领导” 们就这么一直睡着，保安们就这么一直看着，等着，等着“领导”传达指示，等着“领导”前来救场。所以，暴乱和游行是必然要发生的。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;万事开头难，只要有一个人从窗外扔东西，不管扔的是什么，必将有更多的追随者和参与者加入其中，而且花样百出。谁让天这么热，人这么多，心这么烦呢？于是从水壶到饮水机到桶装水，从瓜子到糖果到啤酒瓶，从电脑显示器到主机到硬盘，从槌子到棒子到拖把，光是吉他这么新鲜的玩意儿我就看到了两把，更别提各种垃圾和杂物了。还有鞭炮，火烧棉被，火烧废纸，大家的情绪逐渐高涨，口号也没少喊，最常见的就是骂某贱国(负笑掌)的了，按说事情到了这个地步，但凡是聪明点儿的，赶紧把电送到寝室，赶紧拿高音喇叭控制学生情绪，趁着大家把东西扔的差不多了，中场休息的时候，把事情说开，把姿态放低一些，最起码让学生知道 “领导”们已经重视起来这件事了，正在抓紧处理，兴许此事还有可能就此平息。可惜，“领导”还没给我们辛苦的保安叔叔明确的指示，他们也不敢擅自处理，只能看着，等着，他们很难，一边怕被主子骂，一边怕被学生骂。 所以，更多的人集结到了楼下，一场游行正在随着保安们的不作为而悄然酝酿着。。。。。。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;集结在楼下的人越来越多，各种瓶子罐子火苗子从楼顶飘落，更大规模的暴乱已经不可避免，我曾经这样吓唬我们可爱的保安叔叔“要是有学生被砸伤，是你们负责，还是你的领导负责？要是有人今天死在你面前了，你还能像现在这样笑而不语么？事态正向越来越坏的方向发展，你赶紧把电给我们送来吧~大家也就散了。”保安叔叔很无奈的说：“领导没指示，我们也没有办法。”于是就有了几次冲击传达室的事件，不过咱华理的爷们儿们还是相当的克制啊，没打没抢，就砸砸玻璃烧烧纸片儿，气势是真有了，还合唱了革命歌曲，没有和保安发生肢体冲突，也没有骂保安什么，骂了也没用。<br />
<br />
&nbsp; &nbsp; 浩浩荡荡的游行是在等待领导出现未果，强制宿官大妈输电未遂的情况下开始的。竟然还有人带了哨子，步伐和口号都很齐，大队人马从12号楼那篇寝室区向教育超市那边大一同学的寝室区进发，“鱼煎锅 傻X 鱼煎锅 下课！”气场和声波贯穿整个校园，不知道他本人有没有被吵醒。后来队伍又到了女生寝室区，教职工宾馆区，并冲击了宾馆，期间又有很多人加入到游行大队之中，整个过程给我的感觉那就是俩字儿，解气！<br />
<br />
&nbsp; &nbsp;在我的室友追随大部队冲击宾馆的时候，我和路上碰到的几个负责大一寝室区的老师们商量能不能把电给输了，别耗着了，结果他们的回答和保安一样，“正在和领导联系。”<br />
<br />
&nbsp; &nbsp;一道刺眼的黄光从远处袭来，这气势想必肯定是领导的车了。我心里暗喜，终于来管事儿的了，定睛一看，是一辆很牛拜的桑塔纳两千。从这个人下车的德行我就能感觉到，这货是个芝麻官儿，这货肯定不是煎锅，这货素质估计不高，这货估计是来火上浇油的，事实确实如此。<br />
<br />
&nbsp; &nbsp; 从他和老师们的交流中我得知这位领导姓李。和老师交流过后就跟我和周围几个没和大队伍冲击宾馆的同学打着官腔儿说：你们这种游行是不对地，是错误地，是绝对不允许地。我笑了，我说：“是，这话您一会儿也和那边的同学们这么说么？”“那是当然，一会儿等他们过来的。都回去睡觉吧，都散了吧，散了吧！”<br />
<br />
&nbsp; &nbsp;待到人群聚集过来，这位领导依然然抱着个膀子，一脸的装逼，打着官腔儿说道：“同学们回去睡吧，电一会儿就给你们送过去。”语气像骗弱智一样，没有一点儿真诚，这不是点火么，有人直接就脏话骂上了，我也说：“你现在就让我看见寝室电灯亮了我就走。”这位领导立马翻脸，流氓一样的口气对我吼道，你是哪个班的？！（混哪片儿的？）哪个年纪的？！（混多久了？）你叫什么名字！？（明天就找人收拾你丫的！）我还没来得及说话，旁边就有人嚷道：“你他妈叫什么名字，干什么工作的？说来听听啊！”随即2百多人起哄，丫立刻没电，害怕了。他和他的爱车已经被团团围住，拳头雨点般的砸向他的爱车，划车的，烟头烫车的，摇晃车的干什么的都有，这位芝麻官儿的嚣张气焰瞬间被碾的粉碎，所有人挥着拳头指着他的脸齐声喊CNM！喊了他十多遍，这个家伙连个屁也不敢放，后来就落荒而逃了。<br />
<br />
