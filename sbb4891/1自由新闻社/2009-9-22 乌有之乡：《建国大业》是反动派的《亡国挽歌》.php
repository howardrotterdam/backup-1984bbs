																											<div id="postmessage_252873" class="t_msgfont">《建国大业》：国庆献礼片还是反共宣传片？<br />
<br />
<a href="http://www.wyzxsx.com/Article/Class22/200909/105530.html" target="_blank">http://www.wyzxsx.com/Article/Class22/200909/105530.html</a><br />
<br />
9月16日，电影《建国大业》在全国上映。这一部票价高达百元，由众多“外籍华人”出演的“红色史诗”早在拍摄中已经引起了人们极大关注。今天我想谈谈对《建国大业》的看法。　　<br />
一、一个问题：国民党反动派应该如何拍摄解放战争？　　<br />
有人说，《建国大业》是“一部纯粹的商业片”，“没有任何政治立场”。我是坚决反对这种观点的。即使是商业片也有政治立场，美国的好莱坞大片是典型的商业片，但是无一不体现“美国至上”的“普世价值”。像《亚历山大》中的希腊和波斯显然是影射今天的美国和伊朗，《星球大战》更是把中国、苏联都设成了外国入侵者，《勇敢的心》死前华莱士大呼“自由”，《佐罗传奇》竟喊出了“为加利福尼亚（原属墨西哥）加入美国而欢呼吧”，这难道是“没有任何政治立场”的“纯粹的商业片”吗？事实上，“没有任何政治立场”的“纯粹的商业片”是根本不存在的。《建国大业》拍摄的解放战争的内容，解放战争这个主题是和现实政治密切相关的，其政治立场只会更加鲜明，虽然是故事片，实际上更是宣传片，绝不可能是“没有任何政治立场”的“纯粹的商业片”。　　<br />
那么，《建国大业》的政治立场是什么呢？我认为，《建国大业》的政治立场就是站在国民党反动派角度，是反共反人民的一部反共宣传片。有的朋友可能会说，太夸张了吧，《建国大业》是60年国庆的献礼片，纵有错误，也不能说是一部反共宣传片吧。说实话，我也不敢相信这个结论，因此题目用了个问号。下面听听我的分析，如果大家不同意我的观点，也欢迎批评。　　<br />
判断拍摄的解放战争的文艺作品的政治立场是不是反共宣传片，必须首先弄明白一个问题：国民党反动派逃到台湾之后，应该如何拍摄解放战争？怎样拍对国民党反动派最有利？如果要是丝毫不承认国民党反动派的错误，甚至硬说解放战争是国民党反动派的胜利和共产党的失败，那样不仅达不到反共宣传的效果，徒然惹人发笑。如果希望达到最佳的反共宣传的效果，应该做到以下几点：　　<br />
首先，就国民党反动派方面，必须承认一些错误，主要是腐败问题，但是绝对不能承认国民党反动派是和人民根本对立的这一根本问题。同时，必须宣传蒋介石与蒋经国父子是真心反腐败的，反腐败没有成功是因为腐败深入骨髓，积重难返，和蒋介石与蒋经国父子的态度毫无关系。这样，蒋介石与蒋经国父子就可以在台湾这一刚刚“光复”，腐败尚未深入骨髓，积重难返的地方重新开始。特别是对于蒋经国，一定要极力美化。这样台湾人民即使对蒋介石失望了，还对蒋经国抱有希望，可以让蒋家王朝统治延续下去。　　<br />
其次，就共产党方面，必须承认共产党的一些优点，但是绝对不能承认共产党代表广大人民利益，得到广大人民踊跃支持的这一根本问题。应该把共产党特别是共产党的领导人描述成为一群善用权谋的奸佞小人，通过挑拨离间打败了国民党反动派，这样，尽管国民党反动派失败了，在台湾人民眼中也是值得同情的悲情英雄。另外，最好把共产党的领导人丑化为一群粗俗无礼的土匪，让台湾人民厌恶共产党，使台湾人民跟随已经穷途末路的国民党反动派。　　<br />
再次，应该极力夸大民主党派、无党派民主人士和国民党内部爱国将领的作用。这样可以更好的回避共产党代表广大人民利益，得到广大人民踊跃支持的这一根本问题，把国民党反动派失败描述成内部叛变造成的。这样也可以暗示，共产党的统治是不稳固的，主要依靠民主党派、无党派民主人士和国民党内部爱国将领支持才能维持，而这些人是摇摆不定的中间派，如果国民党反动派占优势可以再倒回来。从而宣传“反攻大陆，反共复国”的“大业”还是有希望的，给逃到台湾的残余力量打气。　　<br />
此外，可以适当的培育“台湾主体意识”，把共产党解放台湾的努力丑化为侵犯台湾人民台湾家园的行径，使台湾人民跟随国民党反动派共同反共。还可以宣传美国的“公平公正”，以为自己的卖国行径辩护，等等。　　<br />
二、两个选项：《建国大业》的主旨是“献礼”还是“反共”？　　<br />
&nbsp; &nbsp; 以上我们说了国民党反动派逃到台湾之后，应该如何拍摄解放战争才能达到反共宣传的最佳效果，下面看看《建国大业》是如何拍摄的，从中我们可以看出“献礼”和“反共”哪个选项才是《建国大业》要表现的真实主旨。以下韩三平先生等人的话主要引自《&lt;建国大业&gt;诞生记：拿什么来吸引80、90后？》中三联生活周刊对他们的采访。特别声明，笔者就是80后，但是一点没有被韩三平先生等人的观点吸引，也许这就是代沟吧，韩三平先生等人并不真正明白应该拿什么来吸引80、90后。　　<br />
1．“末路英雄”蒋介石与“热血青年”蒋经国　　<br />
中国电影集团公司董事长兼总经理，《建国大业》的导演韩三平先生说：“蒋和毛都是英雄”，“张国立有时沉默的时候，脸上有一种末路英雄的状态，有英雄气但又有点茫然”。也许我是孤陋寡闻，献礼片把蒋介石定位为“英雄”，而且是“末路英雄”，恐怕还是第一次。《建国大业》的确充分体现了蒋介石的“英雄气概”，蒋介石在片中变成了一个清廉、正直、一心为国的人，从各个战区的报告得知，林彪的部队抢占东北，陈毅、粟裕的部队威逼南京才不得不反击，面临李宗仁、白崇禧等人谋权夺位的压力“引退”奉化时，还表示不做划江而治的千古罪人。还提出了“贪腐，反则亡党；不反，则亡国” ，一个无奈的“末路英雄”跃然纸上。　　<br />
蒋介石真是个“英雄”吗？我认为那要看对“英雄”如何定义。如果认为“英雄”就是杀人如草、好色如命、饮酒如狂、阴谋层出的黑社会老大式的人物，那么蒋介石不仅是英雄，恐怕还是中国历史上第一人。蒋介石本人就是黑社会出身，先是与陈其美结拜为“盟兄弟”，后来又拜黄金荣为师，靠暗杀光复会领袖陶成章起家，靠“中山舰事件”和“整理党务案”发迹，对待共产党人和民主人士斩尽杀绝、毫不留情，把“盟兄弟”张学良关了几乎一生，对“友邦人士”则异常宽宏大量，抗战刚一结束，马上宣布侵华日军总司令冈村宁次“无罪释放”，并且聘请这位“有8年剿共经验”的“友邦人士”为顾问共商“剿共大计”……可惜这些“英雄事迹”，《建国大业》都没有好好表现。片中表现的蒋介石清廉、正直、一心为国的另一种“英雄事迹”则不是事实，蒋介石统治的时代，广大人民饥寒交迫，即使是被吹嘘为“黄金时代”的1927到1936年仍然有70%至80%的人吃不饱饭，每年饿死300万至700万人，1949年四大家族掌握200亿美元资产，是世界上的贫富差距最大的国家，蒋介石堪称中国历史上第一巨贪。解放战争期间，蒋介石与美国签订了《中美商约》，将国统区变成了美国的殖民地。在败退台湾后，蒋介石还与美国签订了《中美防卫协定》，将台湾以殖民地的形式，置于美国的势力范围之下。清廉、正直、一心为国又从何谈起？　　<br />
《建国大业》把蒋经国塑造成了一个完全正面的人物。《建国大业》的导演韩三平先生说：“蒋经国这个人物，在当年是一个30多岁的青年，就凭他当年30多岁这一条，说他有多么彻底的腐朽没落，他不可能的。他当年还是想励精图治，想改变现状，甚至想推动国家民族发展”，“蒋经国不是一个简单的人。他在苏联卫国战争时候，是拿着冲锋枪跟德国人打过仗的，他是职业军人，跟那种大少爷完全不一样。他身上有许多应该是很优秀的东西，《建国大业》可以说是真实地表现了这种东西”。的确，《建国大业》大力宣扬蒋经国去上海“打老虎”的事迹，蒋经国、宋美龄和孔令侃的一场室内戏，蒋孔这对表兄弟为国事执枪相对，一个英勇的“热血青年”被刻划的栩栩如生。　　<br />
&nbsp; &nbsp; 历史的真相到底如何呢？首先，年轻未必是借口，侵华日军中不少20多岁乃至更年轻的青年同样是狂热的法西斯分子。年轻的蒋经国或许想励精图治，想改变现状，但是所作的只能是维护以他和蒋介石为代表的官僚买办资产阶级的法西斯统治，绝不会触动自己的统治根基。其次，蒋经国根本不是职业军人，他是1937年回国的，苏联卫国战争1941年才爆发，怎会有“在苏联卫国战争时候，是拿着冲锋枪跟德国人打过仗”的“英雄事迹”呢？抗日战争时蒋经国担任过江西省第四区（赣南地区）行政督察专员兼区保安司令，但是不过是蒋介石锻炼他，蒋经国从未去危险的地方。何况以腐败著称的国民党军队，即使真是职业军人身上又能有多少“很优秀的东西”？再次，蒋经国主导的币制改革，实质是对各阶层人民的又一次大洗劫。1948 年8 月19 日发布的《财政经济紧急处分令》规定限期收兑人民所有的金、银、外币，逾期不兑一律没收，大量的黄金、白银、美钞，被政府强制收兑而去。蒋经国去上海“打老虎”实质是依靠暴力掠夺民族资产阶级的财产来维持官僚买办资产阶级的内战。当然，蒋经国也是有贡献的。1949年蒋经国奉蒋介石之命进行中央银行外汇黄金移存台湾事宜，把外汇黄金全部移存台湾。1950年蒋经国担任总政战部主任，正式统筹台湾的情治工作，通过法西斯手段严厉镇压左派和台湾人民要求统一的“民主台湾联盟事件”等运动，对台独势力则大力纵容，一方面于1987年宣布解严，使台独势力的民主进步党合法化，另一方面提拔台独分子李登辉为副总统，成功制造了台独势力尾大不掉的局面。这一切为《建国大业》的明星们去台湾发展创造了良好的条件。对于不少梦想去外国去台湾发展的明星们，蒋经国的贡献难道不大吗？　　<br />
2．匪气十足的“人性化”共产党领导人　　<br />
《建国大业》的导演韩三平先生说：“既然这部电影是丰富的波澜壮阔的历史画卷，那就所有历史人物或者说重要的历史人物都要有所表现，不能忌讳”，“我们在创作时有一个观点：一个人的政治立场和世界观，不能完全和他的道德品质画等号。政治态度没落的人道德品质并非必然败坏，政治态度进步的人，道德品质也并非完美无缺”。《建国大业》也的确是这么摄制的，像惊悉冯玉祥海上遇难后，毛主席摔碗，周总理也勃然大怒，甚至怒发冲冠，拍桌子砸板凳怒斥部下“都是猪脑袋”。还有喜闻淮海战役得胜，中央政治局五个常委酒醉失态，毛主席醉依炕上，四常委醉吼国际歌，把个国际歌唱的非常难听（这应该是改编自刘邦战胜项羽后和部将唱大风歌的传说，以暗示毛主席是刘邦式的善用权谋的“奸佞小人”，蒋介石是项羽式的“悲情英雄”）。甚至还有毛主席开书记处会议熄灭半根蜡烛没，周总理说“这不就成了开黑会了吗”等更加离谱的镜头，让人感觉共产党领导人匪气十足。　　<br />
事实上这种说法是毫无道理的。的确，一个人的政治立场和世界观，不能完全和他的道德品质画等号。政治态度没落的人道德品质并非必然败坏，政治态度进步的人，道德品质也并非完美无缺。但是，一个人的政治立场和世界观的确对道德品质有决定作用。绝大多数政治态度没落的人道德品质同样是败坏的，绝大多数政治态度进步的人，道德品质也同样是高尚的。试问，能指望一个杀人如草、好色如命、饮酒如狂、阴谋层出的黑社会老大式的人物全心全意为人民服务吗？事实上毛主席为革命军队制定了“三大纪律，八项注意”，所过之处，秋毫无犯；国民党反动派的军队到处抓丁拉夫，烧杀抢掠，完全是一伙土匪。至于双方的领导人，国民党反动派多是黑社会老大式的人物，共产党领导人是全心全意为人民服务的共产主义战士，更不在一个层次上。我觉得很奇怪，为什么同样是“去脸谱化”，国民党反动派就要表现“英雄气概”，共产党人就要表现匪气？难道不能把国民党反动派政治态度没落同时的道德品质败坏描述的更深入一些，把共产党人政治态度进步伴随的高尚道德品质摄制的更突出一些？莫非　韩三平　先生内心深处认为国民党反动派的领导人比共产党领导人道德品质更加高尚才是有血有肉、尊重历史事实？　　<br />
《建国大业》不仅有对共产党领导人的丑化，还有对中国共产党整体的丑化。《建国大业》的导演韩三平先生还说：“当时中国大多数人要求什么？要求耕者有其田，要求民主，要求温饱，反对独裁，反对特务，反对法西斯。其实这和西方选举有什么区别啊？你是54%，我是46%，你当总统我不当，这不是一回事儿么？你仔细想通了都是一回事。毛泽东为什么胜利？是他代表更多的人。只不过那时候不是选举，而是采用暴力手段，但是本质上是一样的。所以，英雄、优秀人物怎么才能够获得最后的胜利呢？看你是不是代表着更多人的利益”。《建国大业》也的确是这么摄制的，像影片中就有毛主席“把土地分给农民，使耕者有其田，天下就是我们的”的话。这其实是完全抹杀了中国共产党和国民党反动派的根本区别。　　<br />
解放战争是一场中国共产党代表的广大人民和国民党反动派代表的反人民的官僚买办资产阶级、大地主的阶级斗争，根本不是什么谁代表更多的人的问题，和西方选举是根本不同的。资本主义民主政治本质上是一种金权政治，或者说是权钱交易。大体上呈“我资助你选举，你上台照顾我，下台我再给你一些股票或公司的职务补偿”三部曲，即金钱不是万能的，但没有钱是万万不能的，被选择的双方都是反人民的垄断资产阶级代表，广大人民不过是两害相权取其轻罢了。今天美国，总统大选一半人根本不愿去投票，州长议会选举投票率更低。广大人民可能为支持根本不感兴趣的垄断资产阶级政客而舍死忘生吗？中国共产党也决不仅仅是和历代农民起义一样，通过“打土豪、分田地”，“耕者有其田”建立一个新王朝，而是一开始就以跳出历史周期率，建立没有压迫没有剥削的社会主义和共产主义社会为目标，耕者有其田不过是实现农业集体化，永远杜绝地主阶级复活的第一步，绝不是终极目标。然而，《建国大业》对共产主义理想竟然只字不提。片面强调“耕者有其田”同样抹杀了中国共产党和国民党反动派的根本区别。《建国大业》被剪掉的一场戏中，蒋介石对蒋经国说：“你要把土地分给农民，做到耕者有其田，赢得民心。赢得民心之后，十年之内反攻大陆才有希望”，事实上蒋介石在台湾也的确搞了土改。试问，按照《建国大业》的逻辑，解放台湾岂不成了“不得民心”？不仅如此，《建国大业》还伪造毛主席的话“搞经济我们不行”，“要将资本家请回来”。实际上，中国共产党搞经济比资本家支持的国民党反动派强的多，建国初期就稳定了物价。而且荣毅仁等民族资本家根本没有走，跑到台湾的不过是一小撮极端反动的官僚买办资本家。通过3年解放战争好不容易才把他们赶到台湾了，难道还要把他们请回来吗？　　<br />
&nbsp; &nbsp; 除此以外，《建国大业》丑化中国共产党的细节数不胜数。例如，电影里有个细节，蒋介石和蒋经国谈心，蒋介石说：要是你，你谈吗――都这样了，还谈什么啊……这显然是根本上否定了中国共产党的和平诚意。1949年1月14日毛主席的《关于时局的声明》的八项条件（惩办战争罪犯；废除伪宪法；废除伪法统；依据民主原则改编一切反动军队；没收官僚资本；改革土地制度；废除卖国条约；召开没有反动分子参加的政治协商会议，成立民主联合政府，接收南京国民党反动政府及其所属各级政府的一切权力）并未关闭和平的大门，反而正体现了和平诚意。如果不坚持八项条件，中国共产党和国民党反动派有有何区别？“惩办战争罪犯”只是要明确战争责任，不是真的要置蒋介石、李宗仁等人于死地。这一点蒋介石、李宗仁等战犯也很清楚。事实上不仅傅作义等“战犯”起义后受到了很好的待遇，而且在1949年1月27日，“二号战犯”李宗仁就致电毛主席，表示同意以八项条件为和谈基础，难道他是想自杀吗？　　<br />
3．没有民主党派就没有新中国？　　<br />
《建国大业》的导演韩三平先生说：“毛泽东胜利了，是因为他团结了大多数人，众多的第三党派，众多的风云人物，比如宋庆龄、李济深、蔡廷锴，包括张澜，等等等等，这些人当时在中国政坛都是有影响的人物，各自都影响着一批人。毛泽东团结了他们，就团结了大多数人，所以我觉得这是我们在真实地表现那段历史”。中国电影文学学会会长，北京电影学院客座教授，中国作家协会影视文学委员会委员《建国大业》最重要的编剧之一王兴东先生说：“唯有经过人民政协会议民主选举产生的中央政府才是合法的政府，向世界昭告中华人民共和国的成立是符合中国人民意志的。否则，没有这次政治协商会议，一切都被认为是非法的”，“毛泽东作为中共主席，要实现多党合作，必须请来这三位（宋庆龄、李济深、张澜）非中共的领袖”。《建国大业》整部影片也是围绕人民政协会议召开展开的，特别是注重描写了毛主席邀请宋庆龄、李济深、张澜三人的内容。　　<br />
事实上，这是无限拔高民主党派、无党派民主人士和国民党内部爱国将领的作用反人民的英雄史观。中国共产党代表广大人民的地位绝不是民主党派赐予的，中国人民共和国中央人民政府的合法性也绝不是人民政协会议授予的，中国共产党代表广大人民的地位和中国人民共和国中央人民政府的合法性是广大人民在解放战争用鲜血和生命赋予的。十月革命时列宁和布尔什维克党就解散了立宪会议，由苏维埃制定宪法，这不仅不能说是非法的，而且应该说代表广大人民的苏维埃比各个党派组成的制宪会议合法性更强。在中国人民共和国建立时，民主党派、无党派民主人士和国民党内部爱国将领加起来不过几万人，他们所代表的民族资产阶级及其知识分子不超过中国人口的1%，对于中华人民共和国成立不过是起了一个锦上添花的作用。而且，冯玉祥在苏联客轮上因失火遇难后，马上有程潜补上；阿合买提江、阿巴索夫等人在飞往北平途中因飞机失事遇难后，马上有赛福鼎补上，丝毫没有影响新政协召开。（附带说一句，一般认为冯玉祥、阿合买提江、阿巴索夫等人之死是事故，《建国大业》说成是特务破坏是为突出民主党派、无党派民主人士和国民党内部爱国将领的“英雄气概”。）尽管不能说民主人士可有可无，但是更不能认为民主人士对中国人民共和国建立起了决定性作用。　　<br />
而且，民主党派、无党派民主人士和国民党内部爱国将领的缺点、错误甚至罪行也是客观存在的，不能因为他们参加了人民政协会议就过分美化。以李济深为例，1927年4月初，李济深到上海参加蒋介石召开的反共会议。会后，返回广州，发动“四・一五”政变，派军队包围中华全国总工会广东办事处和省港罢工委员会，解除工人纠察队武装，查封工会、农会、学生会等革命团体，杀害邓培、肖楚女、熊雄等两千多名共产党人和革命群众。《建国大业》尽管对此曾有表现，即毛主席和李济深谈话的时候，李济深说“我杀过共产党，蔡和森就是我杀的（并非如此，1931年8月4日，蔡和森遭国民党反动派广东军阀陈济棠秘密枪杀，而陈济棠1929年就跟时任他上司的李济深分道扬镳了。1929年3月15日　李济深就被蒋介石软禁，1931年“九・一八”事变后才被释放，不可能杀害蔡和森）”，但是后来又为“尊重历史”删除了。而且，在参加人民政协会议之时，少数民主党派、无党派民主人士和国民党内部爱国将领就提出了“轮流坐庄”的要求，尽管因为中国共产党严厉批评有所收敛，但是“轮流坐庄”之心未死，1957年又表现了出来，而《建国大业》对此毫无表现。　　<br />
真正给予中国共产党执政地位和中国人民共和国合法性的广大人民在《建国大业》中则受到了漠视。人民群众创造历史是唯物史观的基础，而这部影片通篇没有讲人民，只讲领袖、党派头目和将军、政客。人民在本片中只是花瓶，只知道献花和鼓掌。据记载，辽沈战役解放军阵亡14009人，伤53328人，失踪1874人，共69211人；平津战役解放军阵亡7030人，伤31478人，失踪936人，共39444人；淮海战役解放军阵亡25954人，伤98818人，失踪11752人，合计136524人。另据记载，三大战役共动员支前民工880余万人次，人民群众出动支前的大小车辆141万辆，担架36万余副，牲畜260余万头，粮食4.25亿公斤。然而，这些在电影中几乎是一笔带过。建国大业变成了一小撮“精英”的大业，而广大人民变成了无足轻重的角色。　　<br />
4．解放战争精神是“兰博精神”？　　<br />
《建国大业》的导演韩三平先生说：“我认为，主旋律影片这个概念是不确切的，应该是主流意识形态影片。主流意识形态影片将来会成为中国电影的主流：你看美国电影，爱国主义，励志，歌颂美好爱情，这都是每个电影的主流，90%都是这种电影，追求美好生活，追求幸福，追求爱情，追求爱国主义，为国家献身。兰博就是为国家献身啊，跟我们的李向阳有什么区别啊？”，“追求阳光，追求向上，追求爱国，追求爱人，追求幸福生活，追求美好爱情，我们这一代和你们是一样的。因此表现和歌颂这些会成为主流”。　　<br />
兰博何许人也？是美国好莱坞巨星史泰龙的《第一滴血》系列的主人公。60年代起美国掀起了很大程度上受了毛泽东思想影响的轰轰烈烈的黑人民权运动，女权运动，反战运动等一系列新左派运动，《第一滴血》系列塑造的兰博形象就是为抵制新左派运动塑造的一个美国法西斯侵略军“英雄”形象。《第一滴血》系列塑造兰博形象是希望通过兰博形象为美国的侵略行为开脱，“兰博精神”实质是一种法西斯侵略精神。　　<br />
解放战争精神和“兰博精神”是根本不同的。爱国主义是有阶级性的，毛主席在《中国共产党在民族战争中的地位》中指出：“有日本侵略者和希特勒的爱国主义，有我们的爱国主义。对于日本侵略者和希特勒的所谓爱国主义，共产党员是必须坚决地反对的。日本共产党人和德国共产党人都是他们国家的战争的失败主义者。用一切方法使日本侵略者和希特勒的战争归于失败，就是日本人民和德国人民的利益；失败得越彻底，就越好。” 李向阳和松井都是爱国主义者，但是一个爱的是广大人民的国，一个爱的是反人民的垄断资产阶级的国。解放战争精神是李向阳式的爱国主义，“兰博精神”是松井式的爱国主义，两者岂容混淆？把解放战争精神说成是“兰博精神”是把解放战争的性质由民族的人民的革命战争歪曲为“侵略战争”，如果解放战争精神是“兰博精神”，那么中国共产党解放台湾的努力就成了侵犯台湾人民台湾家园的行径，应该被谴责了。　　<br />
事实上，《建国大业》中不仅大力宣扬兰博式个人英雄主义，大力宣扬美国式反人民的“普世价值”，而且极力美化美国。《建国大业》中描述了宋美龄赴美求援做最后一搏，待遇大不如前，四处碰壁的情况。马歇尔国务卿义更正言辞的说，我们给你们的贷款是为建设和平的，并不是打内战的，所以不能再给了。《建国大业》中还有蒋介石要空军轰炸开国大典的内容。因为美国人不同意任务飞机在釜山加油，这样如果飞过去轰炸，就一个都回不来，于是蒋介石都“出于人道”毅然取消行动，才使开国大典顺利举行。诸如此类，数不胜数。　　<br />
这是完全违背历史事实，毛主席在《“友谊”，还是侵略？》中指出：“‘战时和战后的对华援助’，据白皮书说是四十五亿余美元，据我们统计是五十九亿一千四百余万美元，帮助蒋介石杀死几百万中国人，也算一项‘友谊’的表示”。如果不是美国大力支持，蒋介石未必敢打内战。毛主席在《别了，司徒雷登》中指出：“那些近视的思想糊涂的自由主义或民主个人主义的中国人听着，艾奇逊在给你们上课了，艾奇逊是你们的好教员。你们所设想的美国的仁义道德，已被艾奇逊一扫而空”，“美国之所以没有大量出兵进攻中国，不是因为美国政府不愿意，而是因为美国政府有顾虑。第一顾虑中国人民反对它，它怕陷在泥潭里拔不出去。第二顾虑美国人民反对它，因此不敢下动员令。第三顾虑苏联和欧洲的人民以及各国的人民反对它，它将冒天下之大不韪”。而且，美国最终还是大量出兵进攻中国了，1950年的侵略朝鲜战争提出“三个月打过鸭绿江去”，派空军轰炸中国东北难道都忘了吗？就算这些都忘了，难道美国前几天要卖给台湾小牛导弹也是为支持中国和平统一的表示？　　<br />
通过以上分析，结合第一部分对国民党反动派逃到台湾之后拍摄解放战争如何达到最佳的反共宣传的效果的分析，可以看出，两者惊人的一致。如果说有什么不同，只能说《建国大业》作为60年国庆的献礼片，显然比国民党反动派逃到台湾之后拍摄的反共宣传片影响更大。难道这还不足以说明《建国大业》是站在国民党反动派立场上反共反人民的一部反共宣传片吗？　　</div>

							
							
							
							
