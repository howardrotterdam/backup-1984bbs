													<h2>乌有之乡：《建国大业》是反动派的《亡国挽歌》</h2>
						
						
																											<div id="postmessage_252870" class="t_msgfont"><a href="http://www.wyzxsx.com/Article/Class22/200909/105550.html" target="_blank">http://www.wyzxsx.com/Article/Class22/200909/105550.html</a><br />
<br />
是人民的《建国大业》还是反动派的《亡国挽歌》？<br />
――《建国大业》逐场景点评<br />
 <br />
田义<br />
 <br />
《建国大业》粗看一遍，零星记下了部分场景，本文试着逐一点评。更多、更深入的内容还需要众网友共同参与进来，不要让它再毒害祖国的未来了。<br />
 <br />
场景1：重庆谈判宴会，国民党军官聊天<br />
国名党将领（李连杰）：还打？中国人打中国人，这种事我不干。<br />
国民党蒋介石搞的是买办资产阶级、官僚资产阶级以及封建地主阶级的独裁统治，国共双方斗争的实质是共产党领导的被压迫的人民大众同国民党反动统治集团之间的阶级斗争，用“中国人打中国人”一语概括，掩盖了阶级矛盾的实质。<br />
白崇禧：马林诺夫斯基已经同意把东北移交给我们了。<br />
军官X：这么说，可以不用打仗了？<br />
白崇禧：不打，到手的地盘共产党会拱手让出来吗？<br />
此段听来怎么像是共产党为了保地盘而同国民党打仗，有把战争的责任推给共产党之嫌。战争的根本责任在于国民党反动派搞反动独裁统治，不给人民生存的权利。<br />
 <br />
场景2：国共重庆谈判记者招待会<br />
中央日报记者：我注意到两位都穿了中山装，……，一样的着装有什么特殊的含义吗？<br />
毛泽东：原因很简单，委员长和我都是中山先生的弟子，国共两党继承的是中山先生民主革命的衣钵，同宗同源，存续相依，中山先生的传人都穿中山装，那是再自然不过了。<br />
首先毛主席是否说过这话要打个疑问，有待核实。即便是有，编剧恐怕也有移花接木的嫌疑：国民党搞的是资产阶级革命，共产党是社会主义革命（新民主主义革命只是过渡，而且有别于孙中山的民主革命），共产党的源头是马克思列宁主义，讲两党同宗同源，是在歪曲共产党的革命宗旨。<br />
 <br />
场景3：国名党高层内部军事会议<br />
白崇禧：目前共军华北一部，山东大部正在向东北渗透，新四军陈毅所部，也在沪杭周边地域活动，给国府还都南京造成了很大困难，我军大部都离这些地区太远了。<br />
原来竟是共产党逼得国民党政权从陪读迁回南京都困难，看来内战的责任的确在共产党。如此安排，编剧也算别具匠心啊，国民党内部会议，不是对外宣传，白崇禧犯不着跟自己人扯淡。编剧完全无视陈毅所部乃至整个八路军力量当时还处在被围困、被压制状态的史实。<br />
 <br />
场景4：毛泽东在重庆会张澜<br />
毛泽东：说句诛心之论，若不是有我们这几十万条破枪，蒋先生会跟我们这些人坐下来谈吗？<br />
毛主席会说“我们这几十万条破枪”这样的话？存疑！“破枪”――作为旁人感慨中国革命之不易，说说也就罢了，编导安排毛主席自己嘴里说出来，明显带着轻蔑的口气。<br />
 <br />
场景5： 双十协定签订后<br />
国民党大员：介公，国府席位三十几个，一下子就让出去八个，让出八个，就得赶走八个，把乌纱帽送给外人，这天下，还是国民党的吗？<br />
蒋经国：父亲，CC他们在私下串联，拒绝承认政治协商会议，并且反对让位给民盟和中共，要是这么闹下去，会出大事的。<br />
挑起摩擦，发动内战的竟是蒋介石身边的人，蒋介石也是被属下胁迫？本片描写蒋介石独裁的镜头很少，为蒋介石洗白的镜头反倒很多，“介公”真该为本片编剧、导演颁发“青天白日勋章”，用电影翻案，这顶的上国民党100万军队。<br />
 <br />
场景6：撤离延安前中央五大领袖开书记处会议<br />
（书记处会议）毛泽东：把土地分给农民，使耕者有其田，天下就是我们的。<br />
打土豪分田地是为了收买民心，夺取天下？看来编剧是被蒋介石的反动宣传机器的阴魂附身了！<br />
周恩来：后勤分队走散了，吃穿都成问题。<br />
（毛泽东熄灭半根蜡烛）<br />
刘少奇：老毛，这开书记处会议呢。<br />
毛泽东：不就是开会吗？开会就是说话，看见看不见都没关系。<br />
刘少奇：这不就成了开黑会了吗？<br />
……<br />
毛泽东：国民党蒋介石大打出手，子弹不长眼啊，我的意见，我、恩来、弼时组成前敌委员会，留在陕北；少奇、老总组成中央工作委员会，东渡黄河去华北。<br />
刘少奇：搞两个中央啊？<br />
毛泽东：鸡蛋不能放在一个篮子里，分开摆放，打烂一坨，还有一坨嘛<br />
虽说毛主席随和、幽默，但如此幽默，恐与史实严重不符。毛主席说用小说反党是一大发明，这年头用电影反革命，丑化人民领袖已经不是什么新鲜事。<br />
 <br />
场景7：蒋介石当选，中共开会<br />
毛泽东：蒋介石在南京当选了总统，我毛泽东就在山沟沟里自封万岁，不好看也不好听，口号二十三条里那个万岁必须拿掉<br />
此段万岁后，语气略有停顿，结合后面的场景19，原来是大有深意。<br />
 <br />
场景8：城南庄被轰炸，厨师为了救毛的食物被炸死<br />
这段戏表现了毛主席厨师的愚忠，实则是封建思想残余，如此便让毛主席由人民领袖蜕变成了封建君王。<br />
 <br />
场景9：国统区通货膨胀<br />
毛泽东：上海滩各种势力虎踞龙盘，国民党内部错综复杂。<br />
周恩来：老蒋做不到的，小蒋也做不到<br />
……<br />
蒋经国：任何人都不要怀疑政府金融改革的决心，国难当头，谁在这件事情上损害了国家的利益，谁就是卖国贼，谁就是我党国的敌人<br />
……<br />
杜月笙：冰山，只怕小蒋先生搬不动吧<br />
四大家族、江浙财团哪个能跟蒋介石脱得了干系，国民政府分明就是老蒋的家政府，这里却把老蒋表现的很无辜，“正直”的蒋家公子形象也跃然荧幕之上，编剧煞费苦心啊。关于这个问题司马平邦在《&lt;建国大业&gt;能看100年还是100天》一文中亦有深刻批驳。<br />
 <br />
场景10：听闻冯玉祥遇难，毛泽东震怒踢翻洗脚水水盆，周恩来怒骂手下。<br />
中共的领袖素质竟是如此的差，沉着稳重的毛主席竟被描述得如此暴躁，周总理也被赋予了一出事就斥责属下的国民党旧官僚形象。<br />
 <br />
场景11：毛周在路上谈话<br />
周恩来：主席，有部分政协人士提出一个建议，由新政协代替全国人民代表大会，直接选举中央人民政府。<br />
毛泽东：战争情况下，很难做到公民普选，但是这件事情必须先征求各党派的意见<br />
众所周知，人民代表大会制度建国后还是确立了，劳苦大众可以自己来当家作主，资产阶级党派要求的资产阶级民主没能取代人民当家作主的地位，那么，这里安排这段话又是何意？编剧是想说“战争情况下”只是借口，毛主席为了笼络民主人士而作出空口许诺吗？<br />
 <br />
场景12：辽沈战役之时，周恩来抽烟，朱德埋首于军事地图，做具体战争部署，周闲得发慌，给朱老总递支烟后便叼着烟出来散步，见毛泽东在屋顶抽烟遥望锦州。<br />
毛泽东：十点半还没冲进去的是哪个部队啊？<br />
周恩来：八纵，这个问题你都问了五遍了。<br />
指挥战争的是朱老总，没毛主席什么事？这种观点似曾相识，“挺朱贬毛”也是当下右翼反动精英的惯用伎俩。周总理抽烟？还是头一回见；毛主席的问话则显得很轻浮。这分明又是在丑化人民领袖。<br />
 <br />
场景13：蒋经国打虎失败，蒋介石跟小蒋谈心。<br />
蒋介石：你说的我全明白，国民党的腐败已经到了骨头里了，反贪腐是件大事，要讲求时机，讲求分寸。难啊，反，要亡党；不反，亡国。<br />
如此明白事理，老蒋的确不简单啊，怕是现在的官员也少有这般觉悟。但这句话也充分暴露了蒋介石的阶级本性：国共之争，何来亡国之说，蒋介石所谓的党只是他的一己私党，蒋介石所谓的国只是他的一己私国。反腐败会动摇蒋介石的个人统治基础，李宗仁等可能会伺机夺权；不反则意味着国民党反动派要彻底失去民心，反动统治要被人民推翻，人民政权将取而代之。作为反面教材，这其实给了今天的官员很好的启示。<br />
 <br />
场景14：傅作义主动联系共产党议和，“关系到近百万人的生死”<br />
历史的真实是：为了和平解放北平，保住千年文化古城，共产党、毛主席大费周章，动之以情，晓之以理，动员傅作义起义；傅作义则是负隅顽抗，死抱着他裂土封侯的白日梦，直到最后绝望关头才出城投降。编剧如此安排，有违史实。北平和平解放主要是共产党的功劳，这是任何人都抹杀不了的。<br />
 <br />
场景15：宋美龄访美，索要美国援助。<br />
马歇尔：美国政府将一如既往地支持蒋介石政府，只是不希望中国的内战继续下去……美国的援助和贷款必须用于和平的建设活动。<br />
“美国的援助和贷款必须用于和平的建设活动”――美国政府可真好啊。宋美龄美貌迷倒白宫服务生，索要美援失败，显得很悲情，观众恐怕都要产生一丝同情了，此种情景下怎么也无法把这位蒋夫人跟中共头号反动派妻子与帮凶的形象联系起来。在凄婉的背景音乐下，镜头切换到蒋介石孤独地坐在专机上看着被人民夺回的江山，此情此景，又有谁会把这个“可怜”的老头跟那个发动“四・一二”反革命争辩的刽子手联系起来呢？全片洋溢着对蒋家王朝的扼腕之情，编导究竟是在为人民拍电影，还是在为反动派拍电影呢？<br />
 <br />
场景16：打到北平城下，王宝强以为是地主大院。<br />
如此恶搞，表现共军很无能。打仗没地图，打到哪里都不知道。<br />
 <br />
场景17：淮海战役胜利后，五大领袖在一起酗酒，喝醉了吼国际歌。<br />
世俗化的处理，如此表现五大领袖，不是编剧低俗就是别有用心。<br />
 <br />
场景18：毛泽东转了两条街，没有商店开门营业，买不到烟，大发雷霆，责怪县委书记成俊。<br />
成俊：进城后，传言说资本家和商人全是剥削阶级，要革他们的命，吓得这些商人买卖全不敢做了。<br />
朱德：这不是个小问题啊，闹革命我们是天下第一，搞经济我们可比不了他们（资本家）。<br />
毛泽东：没有商贩，连香烟都买不到，还谈什么市场繁荣啊，要把人家请回来。<br />
刘少奇：现阶段还不能消灭资本家，生产关系的改变不是过家家，不能胡来。一旦搞出问题，那比在战场上打了败仗还糟糕啊<br />
周恩来：政治协商，协商的对象就是资产阶级政党和民主人士，我们是请人家来共同执政的，不是来消灭人家的。<br />
毛泽东：搞垮了人家，自己又不懂经营生产，工厂倒闭，工人失业，这不是砸自己的饭碗吗？这个饭碗我们刚刚端上，砸不得。<br />
看了这段，发现“没有资本家就不能建设新中国”竟然成了中共五大领袖共识？！共产党进城之时也压根不存在小商贩不敢做生意的情况，历史再次被编导歪曲。如此设计，分明是有人想借演员之口表达自己的观点，颠倒黑白，把“剥削有功”论强加给毛主席，强调资产阶级的“伟大作用”，为今日资改派推行私有化改制，走资本主义道路张目。怎么对待民族资产阶级、手工业者，毛主席在《论十大关系》一文中讲的很明白，但也绝不是要动摇社会主义革命的方向。<br />
 <br />
场景19：毛泽东进京前，红二十八团动员：“今天就两个要求，挺直腰杆，声音宏亮。”再往后看，竟是为了等毛泽东阅兵时喊毛主席万岁。听到万岁声，毛竟为之流泪？！<br />
真实的毛主席是看到人民受苦才流泪，这里的剧情竟是为了一句万岁流泪？到此才明白导演编剧安排场景7的深意。<br />
 <br />
总结：全片表现国民党、蒋介石、民主人士的戏份很多，表现人民解放战争的戏则少之又少，只有零星几个片段，国军将领出来很多，共军将领不多。国民党、蒋介石成了本片的主角，共产党、毛泽东倒似乎成了配角。叫《建国大业》确实严重的名不符实，本片更像是一曲蒋介石集团覆灭的《亡国挽歌》，再拔高一点充其量算是给结尾点题的《政协大会》。<br />
建立新中国，不是资产阶级和知识分子动动嘴皮子开个政协会议讲出来的，更不是蒋介石反动派的让出来的，而是无数革命先烈用生命和热血换来的。忘记过去意味着背叛，制作此片的人，你们站在资产阶级立场上，背叛了人民，背叛了起码的良知和道义；那些竭力污蔑人民共和国历史的人，你们是人民的敌人，今天你们高高在上，可以恣意歪曲历史，丑化人民领袖，替反动派翻案，明天人民必会将你们扫进历史的垃圾堆。</div>

							
							
							
							
