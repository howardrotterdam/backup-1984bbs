													<h2>江西乐安大规模警民冲突 副局长被打跪地求饶</h2>
						
						
																											<div id="postmessage_792235" class="t_msgfont">（参与2010年8月22日讯）：8月16日起，江西省乐安县公溪镇发生警民冲突，几千名当地民众上街抗议，多次被警察殴打，愤怒的民众用石块攻击警察，打伤抚州市公安局副局长元党才。目前上百名村民被抓，冲突已暂时平息。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 公溪镇是原721厂所在地，721厂被称为中国铀都，该厂于上世纪90年代停产倒闭。虽然是铀都，但乐安境内并不产铅。2009年，当地政府在公溪工业园引入浙江华夏电源有限公司投资的江西中金铅业有限公司冶炼厂，项目总投资5.5亿元、年产精铅8万吨，是乐安县重点招商企业，中金铅业的建厂地址是该镇人口密集的罗家圩。该厂污染严重，今年5月起，尚未办完生产手续的工厂开始生产，每天大量排放未经处理的工业用水和臭烟，当地居民告诉《参与》记者，工厂开始生产后，该厂两名工人中毒死亡，附近几所学校的儿童查体也发现铅超标，这位居民还告诉记者，此前已经有人因为抵制该厂落户公溪镇而被判刑，目前尚未出狱。自该厂开始生产以来，为不受污染所害，周围几个村的民众已多次找当地政府，跪求政府官员促使该厂搬迁或实现排污达标，但镇政府领导避而不见，只是叫人告诉老百姓说工厂排出的水和烟没有毒，县里的领导也同样避而不见。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 无奈之下，2010年8月16日，公溪镇多个村庄的村民去中金铅业聚集抗议，要求其搬迁，被乐安县警察及武警驱打，造成多人受伤，其中多为女性及老人。打人事件发生后，愤怒的村民于当晚聚于中金铅业门口讨说法，又与警察发生冲突，造成十多名村民受伤，十多人被抓。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 8月17日，村民继续聚集在罗家圩中金铅业门前抗议，部分村民用手机拍摄现场情况，手机被警察拿走摔坏，再次有村民受伤。由于无法得到政府的正面回应，反而招致一次次殴打，冲突事件升级，17日起，公溪镇几个村庄和部分来自崇仁县的居民（水污染也使崇仁县居民深受其害）封堵了附近的省道，聚集民众达到几千人，政府方面也派了上千警察及工作人员到达现场控制事态，民众与警察发生严重冲突，现场带队的抚州市公安局副局长元党才被村民用酒瓶砸中头部受伤，不得不在大批民众面前跪地求饶，并对村民说自己也是乐安人，请村民手下留情。目前元党才已被送入抚州市第一人民医院治疗。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 8月19日凌晨，当地政府派大量特警逐一进入抗议的村庄，手持电棍强攻进村，很多村民被打伤，警察抓捕了至少一百名村民。目前抗议事件基本平息，被抓的村民没有获释。（陈宇）<br />
