													<h2>“Google图书馆”遭默克尔反对</h2>
						
						
																											<div id="postmessage_280604" class="t_msgfont"><a href="http://guardian.yeeyan.com/guardian/63042" target="_blank">http://guardian.yeeyan.com/guardian/63042</a><br />
<br />
谷歌要建立大型数字图书馆的计划引来了诸多非议。昨天，德国总理安吉拉・默克尔也插手卷入。<br />
<br />
因其对传统出版业、作者和报纸造成威胁，围绕互联网，特别是谷歌的争议正在不断升级。而作为重要的世界级政治领袖，默克尔此次的出面干预也引人注意。<br />
<br />
默克尔是在本周即将开始的法兰克福书展开幕前发表该言论的。在她每周发布的广播演说中，默克尔呼吁国际社会在版权保护上进行更多合作，并表示，她的政府反对谷歌将图书扫描后上传到网络图书馆的计划。<br />
<br />
“德国政府的立场很明确：版权在互联网上也必须得到保护，”默克尔说。她还称，网络版权保护面临“相当大的危险”。<br />
<br />
本周二，默克尔将在德国的财经之都正式揭开世界上最大书展的帷幕。她说，有必要就网络版权保护的问题进行更细致的讨论。引起默克尔如此反应的是谷歌公司提出的逐步将世界上所有的书籍上传到网上以供搜索的惊人计划。虽然这个计划听起来更像是科幻故事，但谷歌一直在网罗全世界图书馆的书籍，把它们扫描成像，然后再把这些图像上传到互联网上，这样一来，使用谷歌便可以搜索到这些书籍。<br />
<br />
有人认为，谷歌至今为止已经扫描了美国和欧洲图书馆里超过 1000万本图书――其中包括剑桥大学图书馆的50万本。扫描图书使用的具体方法还不为人知，而谷歌公司也不允许任何人观看扫描过程。虽然整个过程高度保密，但谷歌声称这项工程的目标完全是无私的。谷歌创始人Sergey Brin和Larry Page曾多次重申，谷歌的终极目标是将全世界所有的信息都放到互联网上。虽然谷歌的目标充满诗意，但这项工程还是招致了很多批评；其中很多批评者认为，这项能提供免费图书的网络服务会挤垮出版业。作者和图书版权拥有者也担心这项工程会侵犯他们的权利，损害他们的盈利能力。<br />
<br />
这件事已经闹到了美国的法庭。纽约的一位法官上周表示，法庭将于11月9日之前对一项允许谷歌将几百万本图书放到互联网上的裁决作出修正。这一举措是为了解决一起由美国作家协会等组织于2005年针对谷歌图书扫描计划提起的诉讼。<br />
<br />
根据原先裁决的条款，谷歌将出资125，000，000美元（约合78，000，000英镑）建立一个图书版权资料库。作者和出版商可以为他们的作品进行版权注册，而注册作品被谷歌发到网上后，作者和出版商可以获得相应的回报。但德国的图书出版商批评欧盟相关部门未能抵制这项裁定。与此同时，法国马帝尼耶出版集团、法国出版业协会和法国文人协会上个月请求巴黎一家法院判处谷歌支付15，000，000美元（约合14，000，000英镑）的罚金，并要求如果谷歌继续“侵犯他们的版权”扫描图书的话，再额外支付每天100，000欧元的罚款。<br />
<br />
谷歌创始人Sergey Brin却认为，谷歌的这一计划将使几百万本绝版图书成为可以利用的网络资源，而不会挤占在售图书的市场，因为那些绝版图书已经在市面上找不到了。谷歌认为，正是得益于这一计划，很多本已难见天日的作品才得以与读者见面。<br />
<br />
谷歌“将世界网络化”的宏大目标在图书之外的很多问题上也掀起了波澜。报业认为谷歌等网站在对新闻内容没有任何贡献的情况下单纯依靠搜罗新闻报道而盈利。而近来几个月里，随着大量记者失业和一些报纸的倒闭，国际报业面临的危机愈发深重，反对谷歌的声音也比以往更加强烈。上周，新闻集团总裁默多克在一篇言辞激烈的演说中指责以谷歌为代表的网络公司是窃取他人成果的盗贼。“过不了多久，那些汇总、抄袭我们新闻的人就要付钱了，”他在北京世界媒体峰会上发言时表示。</div>

							
							
							
							
