													<h2>中国涌现越来越多个性官员 敢说狠话敢说真话</h2>
						
						
																											<div id="postmessage_251125" class="t_msgfont">中国涌现越来越多个性官员 敢说狠话敢说真话<br />
2009年09月21日07:35<br />
<br />
<br />
　　中广网北京9月21日消息 “随着中国大环境的变化，中国官员的言行风格经历了从拘谨到开放的变化。”中国社会科学院国际研究学部主任张蕴岭近日在接受采访时如此概括。<br />
<br />
　　当下，中国官员的变化有目共睹。每年“两会”上，官员越来越“会说话”。各种场合，官员讲话不再照本宣科，见到媒体不再躲躲闪闪，甚至主动递上名片。<br />
　　这种变化上至“庙堂之高”，下至“封疆大员”，让这几年跑时政的记者感觉越来越有“料”了，也让老百姓觉得官员们离自己更近了。<br />
<br />
　　新中国建国初也有一些官员给大家留下了深刻印象。最有个性的当数毛泽东，他的 “铁腕”风格是那时西方不敢小觑中国的重要原因之一。还有当时的国务院总理周恩来、外交部长陈毅，他们的政治智慧和风度翩翩让许多外国政要至今难忘。<br />
　　但那时候普通官员“出头露脸”的机会很少。长期以来中共提倡的“集体决议”、“民主集中”和中国传统的“不出头”、“不逞强”的思维习惯，使官员都湮没在文山会海中，“会议一致通过”――是很长时间以来官员观点的集体表达。<br />
<br />
　　“这与当时中国所处的社会环境相关。”张蕴岭说，中国那时的官员更多是紧张型、拘谨型。<br />
<br />
　　这种沉闷、枯燥的“大多数”形象在一九七八年十月邓小平访日第一次以“西欧方式”见记者时得到了破解，来自世界各地的四百多名记者争相报道了中国新一代领导人的新形象。<br />
　　张蕴岭说，改革开放后，外交官成为首批出现在镁光灯下的官员，而后，大批官员主动走向中国政治经济和国际社会的前台，还原成一个‘人’的形象。”<br />
<br />
　　Jiang Zemin在不同的场合，面对不同的对象，选用不同的语种交谈，一下子拉近了与各国领导人的距离。Hu Jintao春节甘肃剪窗花，一下子拉近了和定西农民的距离。朱F基、Wen Jiabao则在总理记者招待会上分别以睿智、专业、儒雅、深情，向全世界展示了中国官员“智”与“仁”。<br />
　　此后，中国政坛上涌现出一批又一批有个性、有才干、有胆略的“能官”、“强官”、“个性官员”，如原国务院副总理“铁娘子”吴仪；“刚柔相济” 的前外交部长李肇星；敢于“说狠话”、被称为“灭火部长”的前国家安监总局局长、现工业和信息化部部长李毅中；承诺杜绝官场“潜规则”的前商务部部长、现重庆市委书记薄熙来；自称国家财产“看门狗”原审计署长“铁面”李金华；敢于公开怒斥教育乱收费问题的原国家教育部副部长张保庆；数次刮起“环保风暴”的国家环保部副部长潘岳等等，都被归入此列。<br />
　　“这是当今社会多元化使然。”张蕴岭说，这些部委官员所分管的领域容易成为社会关注的焦点，在话语权较宽松的环境下，再加上媒体的渲染，现在的官员比过去有了更多露脸和表达的机会。从敢说话到敢说狠话、敢说真话，让一批官员进入了公众的视野。<br />
<br />
　　谈起自己有印象的特色官员时，中国人民大学国际关系学院教授时殷弘首先想到的是焦裕禄。“焦裕禄虽然打上了那个时代的烙印，但经过几十年的时间检验，他真正成为走在时代前列、做出了一些实事的官员。”<br />
　　对于新时期大批“个性官员”的涌现，时殷弘提醒要理性看待：一方面随着中央放权，地方官员的主动性增强，为地方谋利益是好事，另一方面也要警惕某些官员在媒体面前的“做秀”，最终，还要依靠法律和制度解决问题。<br />
<br />
