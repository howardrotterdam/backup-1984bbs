<br />
　　中国共产党优秀党员，山西省第十届人民代表大会常务委员会副主任、党组副书记薛军同志，因病医治无效，于2010年2月19日9时38分在太原逝世，享年68岁。<br />
<br />
　　薛军同志1942年11月出生于山西省柳林县王家沟乡兴隆湾村。1966年1月加入中国共产党。1962年10月在离石县李家湾高等小学参加工作，1969年9月毕业于山西省委党校政治系党政管理专业，高级统计师。历任离石铁厂革委会副主任、吕梁地区计委干事、地区统计局副局长、局长；1983年9月任吕梁地委委员、组织部部长；1985年7月任省统计局副局长（主持工作）；1986年10月任省统计局局长、党组书记；1992年4月任朔州市委副书记；同年7月任朔州市委书记；1995年12月任山西省副省长；2000年1月任山西省委常委、副省长、省政府党组副书记；2001年10月不再担任省委常委职务；2003年1月任山西省人大常委会副主任、党组副书记；2008年1月退出领导岗位；2009年5月退休。<br />
<br />
　　薛军同志是中共十六大代表，第八届全国人大代表，七届、八届省委委员，省六次、七次、八次、九次党代会代表，省九届、十届、十一届人大代表。<br />
<br />
　　薛军同志对党和人民的事业满腔热情，积极上进。担任离石铁厂和吕梁地区统计局领导职务期间，艰苦奋斗，勤奋敬业，认真负责，作风务实，取得了较好成绩。担任吕梁地委组织部长期间，认真贯彻党的组织路线和干部路线，平反了大量冤假错案。坚持党性，任人唯贤，积极推行干部队伍“四化”建设，选拔了一大批德才兼备、年富力强的领导干部，为吕梁经济社会发展提供了坚强组织保证。<br />
<br />
　　主持省统计局工作期间，薛军同志积极推进全省统计工作改革，提出并实施“提供决策依据、参与宏观调控、实施监测预警”等工作思路，充分发挥统计整体功能，所主持的山西投入和产出法研究和应用达到全国创新水平。山西在全国率先颁布了地方统计法规，统计年报连续四年全国综合评比第一名，省统计局先后获得了多项国家级表彰奖励。<br />
<br />
　　担任朔州市委书记期间，薛军同志团结带领市委一班人，抢抓机遇，深化改革，扩大开放，制定了“确立大思路，实施大动作，实现大突破”的发展战略。加强和改善党的领导，大力推进政府机构改革，坚持正确的干部导向，积极推行组织工作“双层双向”目标管理，激发了广大干部干事创业的热情，形成了推进改革发展的整体合力。抓住煤炭、电力、建材三大支柱产业，以理顺产权关系为重点，以股份制为基本形式，积极推进工业企业改革。在农村改革上，在全省率先实施土地拍卖，有效促进农业产业化、产品商品化、要素市场化，积极探索举龙头、建基地、联农户的农业产业化模式。城市建设加快推进，地区生产总值翻了两番，综合经济实力在全国新建地级市的排名大幅提升，人民生活水平明显提高，促进了朔州经济社会的全面发展。<br />
<br />
　　担任副省长和常务副省长期间，薛军同志认真贯彻落实中央和省委的指示精神，主持制定了山西省21世纪议程、山西经济发展战略，在推进经济结构调整、国有企业改组、民营经济发展、投融资体制改革、对外贸易发展等方面提出了对策措施，为省委、省政府决策提供了重要依据。组织领导了数十项重大建设项目，促进了全省能源产业和基础设施建设的快速发展。组织实施政府采购、会计集中核算和国库集中支付改革，充分发挥财政的调控和保障作用，形成了公开透明、操作规范、高效快捷的财政审批新模式。积极组织企业上市融资，推动资本市场发展，取得明显成效。加强环保工作，突出整顿和治理，大力取缔污染严重的土、小企业，推进重点区域污染治理，使全省生态环境得到较大改善。组织行政执法部门查处假冒伪劣产品，整顿经济秩序，净化市场环境。大力推进旅游产业快速发展，形成了以五台山为龙头、太原为中心的旅游发展格局，全省旅游业总收入有很大增长。分管的其他工作也都有新的发展，为山西经济社会发展做出了重要贡献。<br />
<br />
　　担任省人大常委会副主任期间，薛军同志紧紧围绕新型能源和工业基地建设以及全省经济社会发展的需要，积极推进地方立法工作，主持制定了规范焦炭产业发展的地方法规。有计划、有重点地组织开展执法检查活动，使全省立法、监督等工作取得较大进展。他经常听取人大代表的意见和建议，积极协调有关部门解决人大代表关注的热点难点问题。<br />
<br />
　　退出领导岗位后，薛军同志始终以一个共产党员的标准严格要求自己。退休后仍担任了一些社会职务，经常深入基层调查研究，主动为推进山西的各项工作建言献策，奉献余热。<br />
<br />
