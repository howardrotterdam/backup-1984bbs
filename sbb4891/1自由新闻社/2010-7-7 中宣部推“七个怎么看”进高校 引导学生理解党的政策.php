<br />
　大学生思想敏锐、关注时事，富有激情、求新求异，他们的成长与发展需要正确的理论指导，他们需要实实在在、令人信服、能被他们接受的指导性教材。<br />
<br />
大学生们纷纷表示，通过阅读《七个“怎么看”》能够比较客观地了解就业、医疗，房价等目前大学生普遍关注的问题：<br />
<br />
　作为大学生阅历比较少，但面临着就业、住房、医疗等方面的压力都比较大，所以说，对我们及早了解国家有关政策都有很大帮助。这本书写的非常及时、非常到位。通过这本书的阅读感受到国家和政府正在通过各种努力，解决老百姓和大学生的困难，对此我充满信心。</div>

							
							
							
							
