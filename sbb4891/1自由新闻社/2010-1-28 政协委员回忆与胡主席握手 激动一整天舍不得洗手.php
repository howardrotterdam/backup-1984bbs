													<h2>政协委员回忆与胡主席握手 激动一整天舍不得洗手</h2>
						
						
																											<div id="postmessage_455731" class="t_msgfont"><a href="http://news.hsw.cn/system/2010/01/28/050423999.shtml" target="_blank">http://news.hsw.cn/system/2010/01/28/050423999.shtml</a><br />
华商网：<a href="http://news.hsw.cn" target="_blank">http://news.hsw.cn</a>&nbsp;&nbsp;来源：&nbsp;&nbsp;阳光报&nbsp;&nbsp;2010-01-28 13:55<br />
<br />
政协委员杨乐、郭育文、王巧莉、张建华(从左到右)在和胡主席握完手后,非常激动,回到房间合影。郭育文供图<br />
<br />
　　1月26日，中共中央总书记、国家主席、中央军委主席胡锦涛在陕西视察<br />
<br />
　　工作的时候，来到陕西宾馆12号楼开会。这一天也是今年省政协会议召开的时候，大约几十名政协委员中午开完会回到12号楼，非常凑巧地碰到了刚刚结束会议的胡锦涛主席，委员们都很激动，围在胡主席的身边不肯离去，大家都想看看胡主席，有多名幸运的政协委员和胡主席握了手，大约100多名政协委员看到了胡主席。<br />
<br />
　　昨天，她们对记者回忆了当时见到胡主席的情形。在给记者讲述的时候，每个人还都是那么激动，以至于有的委员说话的音量都高了几度。<br />
<br />
　　记者佟怡高小雨实习生罗莎<br />
<br />
　　政协委员李艳秋：<br />
<br />
　　&quot;和主席握了三次手&quot;<br />
　　政协委员李艳秋一见到记者，就激动地说：“我和胡主席握了三次手！”她一边给记者讲述着见到胡主席时的场景，一边抓住记者的手演示着和胡主席握手的情形。<br />
<br />
　　她很兴奋地告诉记者，自己也没有想到会这么幸运，她用“缘”字来解释这意想不到的幸运。见到胡主席的那一刻，李艳秋说她看到主席的神色略有疲惫，她的脑子里只有一个念头“主席一定要保重身体，健健康康的”，她嘴上不停地说：“胡主席，您多保重！”从见到胡主席到胡主席离开一共只有短短的两分钟，李艳秋委员为此激动了整整一天，中午她也没有像往常一样留在房间睡觉，而是一遍一遍走到遇见胡主席的地方看了又看，她说，自己要记清这里的每一个场景。<br />
<br />
　　她还说，自己的手因为和胡主席握了三次，整整一天都舍不得洗，一直到晚上洗澡的时候，才不得不把手洗了。<br />
<br />
　　政协委员郭育文：<br />
<br />
　　&quot;希望主席多待几天&quot;<br />
　　政协委员郭育文也和其他几位委员一样，原本想着只要能看上胡主席一眼就很满足了。<br />
<br />
　　当胡主席朝他们走来，亲切地和他们一一握手时，她激动得说不出话来，嘴里重复着“主席，您好！”“主席，您好！”郭育文委员笑着告诉记者，抓住胡主席的手，她实在不想放下来，但一想到后面还有很多人等着要跟主席握手，便只好放手了。当记者问到如果有机会再次见到胡主席会说些什么时，郭委员想了想说：“我希望主席在陕西多待几天，看看我们陕西这几年的巨大变化。<br />
<br />
　　政协委员张建华的日记：<br />
<br />
　　&quot;主席问是否影响开会了&quot;<br />
　　今天是来政协开会的第三天了。上午在陕西宾馆10号楼6层参加联组讨论会。12点5分左右，在我们返回12号楼3层319室住地时，看到三层大厅有警卫、一些办公人员和委员数十人，大家都在议论：今天不知能否有幸看一眼我们敬爱的胡主席。我和艳秋、杨乐、郭育文、王巧莉、刘海平、徐泉几个人，决定不吃中午饭，等在这里看一眼胡主席！<br />
<br />
　　我们不舍得放下手中的文件袋，不愿先去宿舍喝一口水解渴，不敢去上厕所，唯恐错过了看一眼胡主席的机会。<br />
<br />
　　12点50分左右，胡主席从12号楼B区第三会议室的中门走出来了。我们远远地望着他，看到我们这些委员，胡主席边向我们招手边走过来，我们大伙就不由自主地跑过去和胡主席握手。我握着胡主席的左手，杨乐和艳秋先后握着胡主席的右手。胡主席微笑着问我们：“你们都在开会？”还问：“我们来是不是影响你们开会了？”我们齐声讲：“没有。”我握着胡主席的左手不放，不住地说：“胡主席好，胡主席好！”胡主席说：“你们开会辛苦了。”胡主席和等在那里的委员们一一握手。<br />
<br />
　　“祝你们的大会圆满成功。”临走的时候，胡主席对我们说。<br />
<br />
　　政协委员林俊丽两次见到胡主席<br />
　　26号上午10点，林俊丽在陕西宾馆12号楼二楼第一会议室参加了提案委员会最后一次会议，对不立案的36份提案进行复议审查。大约到了上午10：50的时候，看到工作人员和警卫在安排让省上领导和胡锦涛主席照相的准备，心情紧张而激动，兴奋不已。<br />
<br />
　　林俊丽说，当时她怎么也按捺不住激动的情绪，“这样受世界瞩目的国家领袖，此刻我就会见到，这是真的吗？不是做梦吧。”林俊丽说，当时，她心里就是这么想的。<br />
<br />
　　于是，林俊丽便悄悄离开会议室，站在2楼的拐角处，焦急地等待着这一刻的到来。10点55分，她看到了胡主席，“和电视上是一样的，显得更精神些。”对于林俊丽印象更深的是，到了中午11：55分，她在12号楼里非常巧合地第二次见到了胡主席，这次主席要从三楼下楼，本来主席要乘电梯下楼的，可是看到很多政协委员在离他不远的地方看着他，他就回过头，双手抱拳，对委员们说：“同志们好！”在场的委员们也都立刻回答说：“主席好！”<br />
<br />
　　“在餐厅，我见到了和主席在三楼握手的委员，大家都很激动。直到现在，我的脑子里只有幸福的回忆。我想，好好工作，作出贡献，争取受到胡主席的亲自接见，一定要用对人民的贡献和成绩来实现这一终生的宿愿。”林俊丽激动地回忆当时的情景。<br />
<br />
