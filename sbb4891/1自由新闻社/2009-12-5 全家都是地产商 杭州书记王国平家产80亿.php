&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;“鉴于王国平同志在城市建设、城市规划、城市研究方面的卓越成果。经研究，特聘王国平同志为中央美术学院客座教授、客座博士生导师。”<br />
<br />
　　 12月12日，杭州市委书记王国平从中央美术学院院长潘公凯手中接过一纸聘书，从此，他有了第二个身份――中央美院客座教授、客座博士生导师。<br />
<br />
　　这样的缘分听起来很奇特，事实上很自然。<br />
<br />
　　目前，各高校都有规划专业，但这样的专业往往只管一个层面，对于城市形象、城市文化、城市经济等并没有系统的统筹机构。因此，中央美院就设有一个专攻城市建设、城市美学的博士生班，班上的十多位学生都是各大城市规委的局长、处长，这个班就由潘公凯亲自授课，目前有一名客座教授是北京市分管城建的副市长。<br />
<br />
　　聘任仪式上，潘公凯高度赞扬了杭州的城市建设。“从城市建设角度，北京可以作为第一个案例，而杭州则是公认除了北京以外最重视规划，也是做得最好的城市。我个人认为杭州是城市建设成功的典型，甚至恐怕在国际上也是成功的典型。”<br />
<br />
　　潘公凯说，在把杭州变得这么优美、这么有文化的过程中，王国平作为杭州的市委书记，无疑有很大的功劳。“王国平是城市建设的专家，希望今后能在博士生班中多起作用。下次，我们会争取把班上的学生凑起来，一起到杭州来上课。”<br />
<br />
　　刚刚接过聘书的王国平笑着说：“感谢中央美院给予我这份荣誉和信任。这应该是属于我们这个团体，属于杭州这个城市的，这给了我们鼓舞和鞭策。”中央美院的博士生班力图破解中国城市化进程中的问题，从城市美学、城市文化，到城市建筑、城市规划，“我非常有幸能参加进来，一定以认真学习的态度，努力工作履行职责。”</div>

							
							
							
							
