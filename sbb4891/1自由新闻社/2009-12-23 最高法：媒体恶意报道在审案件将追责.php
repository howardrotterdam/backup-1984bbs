													<h2>最高法：媒体恶意报道在审案件将追责</h2>
						
						
																											<div id="postmessage_391026" class="t_msgfont">中新网12月23日报道 最高人民法院日前下发《关于人民法院接受新闻媒体舆论监督的若干规定》，要求人民法院应当主动接受新闻媒体的舆论监督。同时，新闻媒体如果对正在审理的案件报道严重失实或者恶意进行倾向性报道，损害司法权威，违反法律规定的，将依法追究相应责任。<br />
<br />
《规定》明确，对新闻媒体旁听案件庭审、采访报道法院工作、要求提供相关材料的，人民法院应当根据具体情况提供便利。记者旁听庭审应当遵守法庭纪律，未经批准不得录音、录像和摄影。<br />
<br />
《规定》强调，对于公开审判的案件，新闻媒体记者和公众可以旁听。有条件的审判法庭根据需要可以在旁听席中设立媒体席。<br />
<br />
同时，《规定》明确，人民法院发现新闻媒体在采访报道法院工作时有下列情形之一的，可以向新闻主管部门、新闻记者自律组织或者新闻单位等通报情况并提出建议。违反法律规定的，依法追究相应责任。<br />
<br />
1，损害国家安全和社会公共利益的，泄露国家秘密、商业秘密的；<br />
<br />
2，对正在审理的案件报道严重失实或者恶意进行倾向性报道，损害司法权威、影响公正审判的；<br />
<br />
3，以侮辱、诽谤等方式损害法官名誉，或者损害当事人名誉权等人格权，侵犯诉讼参与人的隐私和安全的；<br />
<br />
4，接受一方当事人请托，歪曲事实，恶意炒作，干扰人民法院审判、执行活动，造成严重不良影响的；<br />
<br />
5，其他严重损害司法权威、影响司法公正的。<br />
