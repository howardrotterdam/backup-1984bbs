变相“器官买卖”<br />
如今，在搜索引擎上键入“寻求肝源、肾源”，便会有数以十万计的搜索结果<br />
　　除了并发症、对供体的伤害等问题，活体移植还催生了变相的人体器官买卖，这让当初的活体移植的技术发明者始料不及。<br />
　　根据《条例》，活体器官的接受人限于捐献人的配偶、直系血亲或者三代以内旁系血亲，或者“有证据证明与捐献人存在因帮扶等形成亲情关系的人员”。<br />
　　这意味着，除了有血缘关系的亲属可以捐献，非血缘关系的人在一定条件下也可以捐献。<br />
　　这个不够严密的条款，足以使从事活体器官买卖的中介，以及卷入买卖的医院和医生，获得巨大的盈利空间。形式各异的变相器官买卖得以招摇过市，登堂入室。<br />
　　陈忠华分析说，允许配偶捐献器官，却没有对婚姻的长短和质量做出更详细的规定，那就可能存在这样一种情况――为了取肾而结婚，当手术完成以后又遗弃。<br />
　　又如“帮扶关系”。一个富人给一个贫困者提供资助解除其燃眉之急，是否算“帮扶”？给流浪汉管吃管住形成“帮扶”关系后，是否可以理直气壮地让他捐出器官？如果蓄意于此，这和器官买卖有什么本质区别？<br />
　　此外，假证件的泛滥，可谓当前一大“中国特色”。连身份证、毕业证都可以伪造，那么，捏造各种资料，去“证明”两个本不相干的人是“夫妻”“亲属”或者存在“帮扶”关系，对于神通广大的作假者，何难之有？<br />
　　“中国的活体器官移植很大部分都存在买卖关系。”一位不愿意透露姓名的移植业内专家对《财经》记者坦言。<br />
　　卫生部副部长黄洁夫在2008年10月的第四届全国器官移植学术会议上也曾指出，目前社会上存在一些非法的公司，通过互联网或者其他渠道，向医院和患者发出信息，承诺可以办理公证双方属于亲属关系的手续。这些公司的存在，让器官买卖成为可能。<br />
　　如今，在搜索引擎上键入“寻求肝源、肾源”，便会有数以十万计的搜索结果。《财经》记者曾联系网络上的数名中介，自称有朋友32岁，想捐肾换钱。其中有两个中介直接答复说，供者年纪太大，不合适。还有两个中介则表示，32岁男子的肾只能卖三四万元。<br />
　　一名中介还向记者表示，如果真想捐肾，需先做一系列体检证明健康，然后交千元左右的押金以免中途反悔。中介会负责捐肾者的吃住，并尽快安排手术。<br />
　　记者又以肾衰竭患者的身份，去询问是否有肾源。中介表示，可以把他掌握的肾源带过来进行移植，“如果一个不合适，就再叫来两个、三个”。肾源与受体的配型间具有一定弹性，故中介称，只考虑血型符合，一个肾六七万元；如果追求更好的配型吻合，更大幅度降低排异风险，则需要13万至15万元。<br />
　　在活体移植中，配型和抗排异药物是患者术后生存质量的重要因素。所谓“配型”，是指器官供体与受者之间组织抗原的相似性。相似性越高，器官移植以后出现排斥反应的几率越低。<br />
　　这意味着，中介买卖一个肾的差价，当数以万元计。<br />
　　为了丰厚的利润，这位中介还一直向记者声称，肾移植比血液透析更为优越。当记者表示担心医院不会同意手术，中介还告诫记者，“就说卖肾者是你的亲戚”。<br />
　　目前，许多中介已经发展到成熟的公司化运作。他们分工有序，各司其职。据陈忠华分析，这些中介的崛起期，正是在2007年最高人民法院回收死刑核准权之后。中介主要由三类人构成：一部分人以前做“境外旅游移植生意”，一部分人以前倒卖移植专用药物，另一部分是接受过移植的患者或其家属，他们都熟悉“那一套流程”。<br />
　　由于活体移植“市场”的带动，中国的活体移植年龄趋于年轻化。根据中国肝移植注册系统的数据，肝移植供者的平均年龄只有29.2岁。很多中介只寻找年轻人作为器官供体，一是为了保证供者的安全，二是为了让受者保持好的性功能等身体功能。<br />
　　这是一个残酷的事实：那些20多岁、来自穷乡僻壤，或者一时陷于困境走投无路的年轻人，不幸沦为他人的“器官库”。他们或自愿、或并不那么情愿，走上了冰冷的手术台；手术刀起落之间，鲜活生命的一部分随之流逝，背后则凸显不断加大的贫富差距之痛。<br />
<br />
利益链<br />
在器官移植的“黑市”，需要供者、受者、中介、医院、医生密切配合，各担其责，如此构成了一个完整的链条<br />
　　相对于走私、贩毒、卖淫等社会不良现象，“器官买卖”的操作显得更为复杂，因为这是一项技术含量极高、流程极为缜密的系统工程。仅时间挑战就格外严峻：器官不能在死者身上留存，如果供体已经心脏死亡，那么必须在一两个小时内把器官取出，否则无法使用。<br />
　　因此，在器官移植的黑市，需要供者、受者、中介、医院、医生密切配合，各担其责，如此构成了一个完整的链条。<br />
　　驱动这根链条运转的，无疑是金钱；掺杂在这根链条之中的，还有权力的滥用、体制的弊端、道德的沦丧和弱势者的无奈。<br />
　　以肾移植为例。如果肾来自死囚，从患者检查、入院、手术到出院，一般收费6万至10万元。此后，每年需要花费3万至10万元服用抗排斥药物，其中第一年较多。除去药费、检查费和“公关”器官的花费，移植中心与医务人员尚有结余。<br />
　　在器官捐献工作会议上，黄洁夫坦言，做器官移植，医生是有收益的。更何况还可能有患者私下送的“红包”。<br />
　　如果移植的器官来自活体买卖，患者还将支付10余万元“买肾钱”，其中大部分由器官中介赚走，卖肾者往往只能拿到3万至5万元的“报酬”。有些“好心”的中介会为卖肾者向患者再申请几千元的“红包”和营养费。<br />
　　“当买卖器官的利润以‘万元’为单位、甚至能达万级两位数的时候，人们就会疯狂。”陈忠华说。<br />
　　除了金钱利益，还有一些因素也促使临床医生放松对活体移植的审查，甚至给不具备移植手术指征的患者进行手术。例如，陈忠华认为，在卫生部门的移植医院资格评审中，手术量是指标之一，医院需要一定的手术数量以维持资格。<br />
　　不过，黄洁夫强调，手术数量只是复审的非主要参考指标之一。复核的主要指标，是看医疗机构和医务人员是否忠实地按照《条例》办事。<br />
　　要切断这个利益链，《条例》的修订以及有效的执行与监管必不可少。陈忠华建议，一定要做好供者的身份审查，医院的器官移植伦理委员会也应该独立于医院院长的影响，认真把关。此外，对于夫妻间的活体器官移植，应考虑设置婚姻年限；对于“帮扶”关系，还应明确“帮扶”性质、方式和时间长短等要素。<br />
<br />
“旅游移植”<br />
“那些利用中国公民器官谋利的行为，破坏了中国融入器官移植国际主流的努力”<br />
　　在器官移植的立法和监管方面，中国远滞后于其他国家。同为发展中国家的印度，在1994年已经通过《人体器官移植法》；至于英美等国，颁布实施器官移植法律的时间更早。中国直到2007年5月，《人体器官移植条例》才开始实施。<br />
　　长期以来，中国开展器官移植的医院多达数百家，可谓鱼龙混杂；移植费用也比西方国家低，使得中国一度沦为“器官移植旅游”的天堂――不少外国人以“旅游”之名，到中国接受器官移植。<br />
　　2005年11月，《财经》记者曾在菲律宾首都马尼拉采访器官移植国际大会。世界卫生组织官员在会上展示了很多发布于网络、名为“旅游”实为器官移植的非法广告，这些所谓“旅游”的目的地正是中国。一些医院在自己的网站上对器官移植公开明码标价，有的甚至在境外设立专门的联络点。<br />
　　美国洛杉矶加州大学医学院研究人员2008年10月发表的一篇论文也印证，中国是“器官移植旅游者”的首选目的地：一部分美国患者在其他国家接受移植后，会因为种种原因到该医学院继续接受治疗。而这些患者中，近一半是在中国完成的移植。<br />
　　2009年初，媒体曝出“17名日本人在中国通过旅游方式接受器官移植”，激起轩然大波。<br />
　　这个消息最早出现在日本共同社的报道中。报道称，自2007年中国卫生部门原则上不允许给外国人进行器官移植以来，仍然至少有17名日本人在中国接受了肾脏和肝脏移植手术。这些患者通过一家负责器官移植的日本组织牵线，在广州的一家医院接受了移植手术。平均每位患者的费用接近人民币60万元，其中包括向医院和医生支付的费用以及差旅费用。<br />
　　报道还称，这些患者应中国医院的要求用中国人的名字住院，而提供器官的人可能多是死囚。<br />
　　世界卫生组织官员诺埃尔博士对这种现象感到痛心：“那些利用中国公民器官谋利的行为，破坏了中国融入器官移植国际主流的努力。”<br />
　　2007年7月，中国卫生部重拳出击，在一份文件中要求，器官移植优先满足中国公民的需要，外国居民到中国接受器官移植，医疗机构必须得到卫生部相关管理机构的特许。<br />
　　在2008年10月举行的第四届全国器官移植学术会议上，卫生部副部长黄洁夫就向与会者通报，有三家医院仍然擅自为外国人提供器官移植服务，所在医院和医生均受到处罚。<br />
<br />
罪恶无底线<br />
三年前，河北省行唐县发生了一起与器官买卖有关的血案，举国震惊。农民王朝阳伙同他人，将一名流浪汉杀害，然后将其器官出售给几位医生<br />
　　器官供需的极度失衡，器官买卖的地下市场，甚至诱使一些人铤而走险。今年在贵州发生的“杀人盗器官”案，肯定不是第一起类似案件。<br />
　　三年前，河北省行唐县就发生了一起与器官买卖有关的血案，举国震惊。该县农民王朝阳伙同他人，将一名流浪汉杀害，然后将其器官出售给几位医生。<br />
　　这名40岁的流浪汉名叫仝革飞，行唐县龙洲镇东庄人，15岁那年精神曾经受过刺激。2002年父亲去世后，仝革飞再度受到刺激，便到行唐县城流浪乞讨。<br />
　　根据石家庄市人民检察院的起诉书，被告人王朝阳与三位同伙，在2006年11月9日晚，将乞丐仝革飞捆绑、拘禁在行唐县上方村南的废弃旧变电站院内的房子中。之后，王朝阳找到广州一位中介，然后辗转联系到几位医生，谎称在法院和监狱都有熟人，有一名犯人将被执行死刑。<br />
　　11月15日凌晨5时许，被告人王朝阳在废弃旧变电所内先将仝革飞勒死，之后欺骗连夜赶来的医生，称死者是被法院刚执行完死刑的人员。几位医生随即摘取了仝革飞的器官，并付给王朝阳1.48万元。<br />
　　这几位医生在返回途中觉得不对劲。他们开始怀疑：如果是死刑犯，为什么没有穿囚衣，也没有检察院、法院的人在场？于是，他们分别打电话给各自的领导通报情况后，决定报警。随后警方抓获了王朝阳。<br />
　　仝革飞下葬的当天，医生们通过行唐县刑警大队，支付给仝革飞的亲属6.5万元作为补偿。而他们后来只是以证人，而非被告人的身份出现在法庭。<br />
　　这一案件发生时，《人体器官移植条例》尚未出台，几位医生也在事发后主动报警，但人们仍对他们的行为提出了质疑。毕竟，几位医生摘取人体器官的地点与相关规定存在明显出入，而且为获得器官付了费。<br />
　　广州市律师协会医疗纠纷法律专业委员会副主任委员宋儒亮对《财经》记者说：“目前要获得器官供体，都是上门主动去‘收’，这是器官捐献走样的一种表现。在混乱无序的情况下，医生也可能触碰刑律。”<br />
　　从某种意义上讲，杀死仝革飞的不是王朝阳及其同伙，而是在很大程度上依赖于死囚和买卖的器官移植体系。这是赤裸裸、血淋淋的罪恶，它触及了人类法律和道德底线。<br />
<br />
初创捐献体系<br />
人体器官捐献体系着手创建，并首先在上海、天津、辽宁、山东、浙江、广东、江西、福建厦门、江苏南京、湖北武汉等十个省市进行捐献工作试点<br />
　　中国器官移植的处境，逼迫主管部门不得不推动改革。<br />
　　器官移植是一种救死扶伤的医学技术，其本身的发展和成功，是当代医学飞跃性进步的一种体现。然而，移植行为事关供体和受体双方的生命神圣性，器官移植事业的大步发展，必然牵动社会政治经济制度、医疗体系、社会伦理、公民社会建设等方方面面。<br />
　　当中国已经成为世界第二器官移植大国，当每年一万来例移植手术、上百万受体等待已经是现实，如何面对器官移植的供求冲突，建立公平分配机制，使其不再是神秘不可触的“特殊敏感领域”，是改革必须面对的目标。<br />
　　近年来，有关领域的管理层为推动中国器官移植体系的建立与规范，也进行了大量努力。从《人体器官移植条例》颁行，到卫生部门提高医疗机构开展器官移植的准入门槛，中国器官移植逐步迈向法制化、规范化，开始赢得国际社会的好评。<br />
　　但是，解决器官紧缺问题的根本出路，在于建立符合国际伦理原则的人体器官捐献体系，扩大器官供给。共识于此，在8月25日召开的全国人体器官捐献工作会议上，中国红十字会总会和卫生部宣布：将着手建立人体器官捐献体系，并首先在上海、天津、辽宁、山东、浙江、广东、江西、福建厦门、江苏南京、湖北武汉等十个省、市进行捐献工作试点。<br />
　　此次构建的中国人体器官捐献体系计划，由国家和省（区、市）两级机构组成。两级机构都包括捐献委员会、捐献办公室、获取组织和专家组。体系的最高领导机构是中国人体器官捐献委员会，由中国红十字会总会长彭云任主任委员，卫生部副部长马晓伟、黄洁夫和红十字会总会常务副会长江亦曼任副主任委员。<br />
　　根据试点方案的草案，各省红十字会应建立器官捐献办公室，派出协调员，到各个医院寻找潜在的捐献者，例如重症监护室中的患者，并征求其意见，进行登记。当捐献者达到待捐献状态时，捐献办公室和获取组织根据一定原则分配器官。红十字会还将适时建立器官捐献基金，用于开展宣传动员，对捐献者的遗属进行人道抚恤。<br />
　　卫生部人体器官移植技术临床应用委员会委员、北京协和医学院生命伦理学研究中心执行主任翟晓梅说，器官捐献是无偿的，但这并不等于捐献者及其家属不能得到合理的激励。激励的形式可以多种多样，例如感谢信、徽章。对于符合条件的捐献者家属，红十字会可以给予其适当救助。<br />
<br />
转折关头<br />
需要培育出一个中立、客观的第三方，具有高超的组织能力、管理能力，需要透明公开的信息和法治环境的完善<br />
　　根据先进国家的经验和做法，国家器官移植工作体系，应包括国家器官移植管理体系、人体器官捐献者登记系统、人体器官捐献和分配网络体系、器官移植临床服务体系、人体器官移植科学登记系统五个部分。<br />
　　这一套捐献、分配体系，如果能够建立并有效执行，将填补当前中国器官移植体系最大的空白。但要做到这一切，需要培育出一个中立、客观的第三方，具有高超的组织能力、管理能力，需要透明公开的信息和法治环境的完善。这对中国的法治建设和公民社会的成长，提出了迫切的要求。<br />
　　卫生部副部长黄洁夫曾指出，供体来源不足不仅是传统观念所致，更重要的是，尚未从法律层面进一步明确和规范器官捐献的方式和途径。捐献者以及受者的权利和义务，也无法得到有力的法律保障。<br />
　　在某种意义上，器官也可称之为一种资源，其有限和稀缺性必然牵动各方利益，其中也包括地方利益。现实中，由于缺少区域性器官移植登记、分配的协调管理机构，没有器官供受分配的网络体系，器官的获得和分配“各自为政”，既导致了低效，也加剧了分配不公。<br />
　　一位器官移植专家透露，对于器官的分配，地方上有个不成文的规定――本地供体只允许提供给本地医院，因此器官供体实际上被各地垄断。而要想跨地域获取器官供体，当地医院就成为最重要的“公关对象”。一些医院有器官来源而找不到配型合适的受者，器官被白白浪费，另一些医院却器官来源匮乏。<br />
　　故此，把中国所有的器官捐献与需求信息统合在一起，通过消除信息不对称，保障捐献器官使用率的最大化，是很多业内人士的期望。<br />
　　从理论上讲，有了信息平台，就可以把各个医院器官移植的供求信息统合起来，提高器官分配的效率；而且由平台统一给患者排序，也可以避免医院“暗箱操作”、不当移植，提高器官分配的公平性。<br />
　　这其实涉及又一个伦理问题――谁最应该接受器官移植？<br />
　　现实中，中国一些医院进行的某些器官移植手术，会忽略公认的医学标准和伦理原则；接受者的身体条件可能并不太适合接受移植，或者身体条件等因素明显不如其他同样在等待器官移植的患者。<br />
　　浙江大学医学院公共卫生系教授施卫星等人在《中国医学伦理学》杂志发表的一篇文章中指出，器官移植资源应该遵循公平、公正和公开的原则，即从社会公众利益最大化出发，依据移植的迫切性、必要性、成功的可能性，以及预期寿命的长短等医学标准确定移植次序，而不因财富、权力的多寡而有所偏向。<br />
　　在美国，有专门的非盈利机构“器官共享联合网络”（UNOS）负责记录并公开全美所有器官移植手术的状况，器官获取和移植网络（OPTN）则负责记录器官获取的情况。每例尸体供者器官的资料都要与全国受者进行匹配，根据供、受者所在地区远近，血型组织相容性、等候时间、受者年龄等因素进行分配。<br />
　　根据卫生部和中国红十字会即将开展的人体器官捐献体系试点方案，捐赠器官的分配，将由各省份的捐献办公室和获取组织共同进行。<br />
　　北京协和医学院教授翟晓梅对《财经》记者说，如果器官捐献、分配由卫生部门一家来操作，会有利益在里面，“所以要引入红十字会这样的社会团体”。<br />
　　不过，在引入红十字会之后，如何出台具体的措施，保证器官分配的公平，仍然荆棘丛生。在当前医疗机构“垄断”患者信息、红十字会缺乏足够专业知识的背景下，两者能否有效地跨部门合作并互相制衡？当急需器官的患者缺乏支付能力时，医院会否将器官用于其他人？<br />
　　对此，有专家建议，可以将需要器官移植的患者信息录入排名系统，移植器官分配由电脑程序完成。<br />
　　黄洁夫则明确表示，在器官分配上，会优先考虑病情的轻重缓急。“建立捐献体系，就是希望维护公平性和可及性。”他强调，“我们必然会碰到困难，需要大家‘摸着石头过河’。”<br />
　　全国人体器官捐献工作会议结束之后，与会者仍在关注贵州发生的“杀人盗器官”案。目前，警方尚未公布案件细节，中山大学附属第三医院及涉案医生将受到何种处理，也还属未知。<br />
　　毫无疑问，这一案件将在很长时间内，成为中国器官移植界难以承受之痛。而人们更需要面对的，是中国器官移植的困境，以及困境背后折射的体制漏洞。如黄洁夫所说，中国的器官移植事业正处于生死存亡的关头。<br />
　　陷入此境的中国器官移植界，需要一场彻底的、持久的拯救。“杀人盗器官”这类极端案件敲响的警钟，卫生部门变革的决心，人体器官捐赠体系的启动，能否为器官移植事业带来转折？<br />
<br />
多年的积弊不可能在短时间内消除。需要经受考验的，不仅是中国器官移植界的智慧和能力，更有整个社会的制度、道德和良心。■<br />
　　编辑说明：《财经》获悉，十个试点省市要在2010年才正式开始运行捐献体系，但有意愿捐献器官的热心人士，仍可以联系自己所在地的红十字会，登记遗体或器官捐献。《财经网》将会提供各地红十字会的联系方式。详情请见<a href="http://www.caijing.com.cn" target="_blank">www.caijing.com.cn</a><br />
<br />
<br />
国外器官移植一览<br />
　　自1954年肾移植在美国波士顿获得成功以来，人类已能移植除了人脑的几乎所有重要的组织和器官。其中，心脏移植一年以上的存活率在70％以上，肝脏移植则达88％，接受移植后最长存活时间为31年。<br />
　　在美国，一位质量良好的供者至少可使75例患者受益，肝脏、肾脏、心脏、胰腺、肺脏、小肠等器官和角膜、皮肤、心脏瓣膜、骨、骨髓、静脉、肌腱、软骨、韧带、筋膜、硬脑膜等组织可用于移植。<br />
　　据世界卫生组织统计，在2005年开展的6.6万例肾移植手术中，60％在工业化国家；在2.1万多例肝移植和6000例心脏移植中，75％是在工业化和新兴经济体中开展的。目前，中国已累计开展器官移植8.5万余例，成为数量仅次于美国的第二大器官移植国，当前世界上所有的移植技术几乎都能在中国进行。<br />
<br />
供体来源<br />
　　在国外，同种器官移植的主要来源是死亡病人。美国2006年开展的器官移植手术中，尸体移植占76.2％，活体移植只占23.8％，约为尸体捐献的三分之一。捐献者可以通过本人或者家属，来表达捐献的意愿。<br />
　　在欧洲，大多数国家在器官捐献方面采取“默认同意”的原则。也就是说，除非人在生前明确拒绝器官捐赠，否则医生便将其默认为同意捐赠器官。在法国，有专门的医院登记机构记录人们拒绝捐献器官的要求，比利时则由全国登记机构负责记录。但严格遵守这种制度的国家并不多见，更多的国家，如西班牙、意大利等，则是在“默认同意”原则的基础上，还须获得潜在捐献者家属的同意。<br />
　　另一些国家，如加拿大、英国、荷兰、土耳其等，采用的是志愿捐献器官体系。在这种由人们主动选择捐献与否的制度中，只有在死者生前同意并且死后遗属同意的前提下，医院才有权摘取器官。<br />
　　美国确立的则是“必需请求”的捐赠制度。器官捐献是公民参加医疗保险时必须作答的自愿性选择，并说明是否授权给自己的近亲属或者委托代理人行使撤销权。<br />
　　至于活体器官的捐献，多数国家通过立法的方式，明确活体捐献要以供体的生命和健康不会因此受到损伤为前提。<br />
　　土耳其在1979年颁布的法令中规定，“如果摘取器官会引发供体的死亡或者危及供体的健康，则禁止器官或组织的摘取。”西班牙、挪威、瑞典、丹麦、塞浦路斯等国家的法律中亦有类似条款。比利时法律还规定，如果从18岁以下的未成年人身上摘取不可再生性器官，则必须保证摘取不会严重影响捐献者的健康。<br />
　　在对捐献者与接受者之间的亲属关系限制上，各国之间的差异较大。在美国，不论尸体或活体移植，供体与受体之间没有亲属关系限制，未成年人经父母或法定代理人同意亦可捐献器官。<br />
　　而在法国，捐赠者与接受者须为两代以内的亲属，如父母、子女、兄弟姐妹，急迫情形之下可包括配偶；原则上禁止未成年人捐赠器官，违反者将处以刑事制裁，例外的情况仅限兄弟姐妹之间的骨髓移植，但仍须符合一定条件。<br />
<br />
器官管理<br />
　　当前，世界各国都面临器官来源不足的问题。在西班牙等器官捐献率较高的国家，情况稍好。大多数国家成立专门机构来管理和分配供体。该机构负责构建统一器官捐献登记网络或资料库，公众可随时获取其中的信息。为了防止患者与捐献者直接接触，双方的个人信息均受到严格保密，从而保证器官分配的公正。<br />
　　西班牙卫生部成立的国家移植协会，国际上称之为“西班牙模式”，欧盟推荐成员国采用该模式，认为它是惟一被证明有效的可增加捐献器官数量的方法，并得到了澳大利亚、巴西、19个拉丁美洲国家和美国相继学习和采纳。<br />
　　西班牙国家移植协会是以医院为基础的捐献系统，由一组经特别培训的人员组成，在全国范围内实施标准的器官捐献程序。它类似于一个网络系统，由三个级别的协调组织构成――国家级、区域级和医院级。<br />
　　美国在经历了一段器官无序化管理之后，现已逐步建立了比较健全的器官移植系统。美国国家器官移植系统以医学和科学的标准来管理器官分配政策，不容许有种族、性别及经济收入方面的歧视和政治权利方面的徇私。同时，每例尸体供者器官的数据资料，都要上传至器官资源共享网络（United Network for Organ Sharing，UNOS）的国家计算机中心，通过与全国受者进行匹配后，再决定器官分配的优先权。<br />
　　按照美国国家器官获取和移植网络（Organ Procurement and Transplant Network，OPTN）以及UNOS的规定，每个器官系统都有专门的科学评估方法，并随时删除不适合移植的受者。<br />
　　英国的器官管理体系与美国类似，所有需要器官移植手术的患者须在“英国国家器官移植资料库”中登记，配型因素包括血型、年龄和供体与受体的身量等。■<br />
　　<br />
本刊实习记者 王璇/文<br />
