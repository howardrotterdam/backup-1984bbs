死囚伦理<br />
这是一个官方讳莫如深亦人人心照不宣的事实――死刑犯被执行死刑后的尸体器官，是中国最主要的移植器官来源，长期占到90％以上<br />
　　对于中国的普通百姓来说，人体器官捐献还是一件稀罕事。<br />
　　华中科技大学同济医学院教授陈忠华日前对媒体披露，从2003年到2009年8月，中国内地仅有130位公民逝世后成功捐献器官。2007年以来，中国内地每年开展临床器官移植约1万例，其中来源于公民逝世后捐献的器官不到30例。<br />
　　而在黄洁夫看来，中国根本还没有器官捐献体系，有关捐献的数字也就无从说起。<br />
　　于是，问题无可回避地出现了：既然器官捐献体系是空白，中国每年大量用于移植的器官来自何处？<br />
　　这是一个官方讳莫如深亦人人心照不宣的事实――死刑犯被执行死刑后的尸体器官，是中国最主要的移植器官来源，长期占到90％以上。<br />
　　在官方公开的文本中，公众很难获得死囚器官摘取的具体信息。但从媒体过往几年的零星报道中，仍可发现一些端倪。<br />
　　《金陵晚报》曾报道称，著名影视演员傅彪2004年9月进行了一次肝移植，所移植的肝脏来自山东一名20多岁的年轻死囚。<br />
　　有时，死囚器官被摘取以后，家属并无法知道其是否真正愿意捐赠器官。<br />
　　人民网曾报道，2000年5月，江西的付信荣因犯故意杀人罪被执行枪决后，迅即被省城某大医院的面包车拉走。其姐姐付木兰怀疑，弟弟的尸体器官被当地法院私下出卖，于是聘请了律师，欲将法院告上法庭。<br />
　　2003年4月，甘肃的杨自荣在敦煌因故意杀人罪被处决后，家人听说他的肾脏被取走了。杨自荣的父亲杨天召没有见到儿子同意捐赠器官的书面材料，为儿子收尸的要求也被拒绝，他甚至不知道儿子到底被取走了什么器官。《兰州晨报》记者在敦煌市看守所采访时被告知，杨自荣只是“口头提出了（捐赠）申请”。<br />
　　一位不愿意透露姓名的器官移植专家告诉《财经》记者，中国每家器官移植医院都有专职人员负责“公关”，他们会和各地的司法、公安部门联系，寻找死囚器官来源。<br />
　　又据一家大型医院器官移植中心的负责人介绍，器官移植的程序大体是：医院先在供体当地卫生主管部门、公安机关备案；医院与所在地公安机关进行联系，提出需要器官供体的申请；公安机关通知医院去领取供体；医院取得供体后对器官加以利用。<br />
　　卫生部副部长黄洁夫深知中国器官移植界存在的“潜规则”，也正是他，在2005年7月召开的世界肝脏移植大会上，代表中国政府首次正式承认了器官更多来源于死囚的事实。但他同时指出，中国政府严格遵循世界各国普遍遵守的伦理学原则，并有根据中国国情和文化背景指导器官移植的相关理论；摘取死囚器官的原则是征求死囚或者他们家属的同意，并且对死囚实行人道主义待遇。<br />
　　毕竟，主要依赖死囚渠道获得移植器官，这与中国的社会进步极不相称。正因为医学界一直无法说明移植手术的供体来源、信息不透明以及回避参与讨论器官捐赠的伦理问题，中国的临床专家长期难以登上国际讲台介绍移植经验。<br />
　　当然，并非只有中国利用死囚器官。事实上，在世界各国，关于死囚器官的利用一直充满伦理的冲突。有人对死囚器官利用表示有条件的支持，他们的逻辑是：死刑犯器官不利用是一种浪费；如果犯人确实自愿在死后捐出器官，既可以使他们的心灵得到慰藉，也为他人提供了活命的机会。<br />
　　随着社会文明的进步，更多的是反对之声。世界卫生组织负责器官移植的官员吕克・诺埃尔（Luc Noel）博士告诉《财经》记者，反对利用死囚器官者认为，置身于监狱中的死囚，很难保证他们真正拥有自愿选择的自由；死刑的执行日期可能会迎合器官移植的需要，从而影响司法公正；对于死囚器官的控制，也可能带来司法系统的腐败。<br />
　　广州市律师协会医疗纠纷法律专业委员会副主任委员宋儒亮在其2008年出版的《脑死亡与器官移植――关联、争议与立法》一书中说，获得器官移植供体需求的冲动，或许会干扰最高人民法院收回死刑复核权来实现“保留死刑，少杀慎杀”之努力。种种问题叠加在一起，可能会使死囚器官资源利用更注重效率而忽视公平，更注重医学而藐视法律。<br />
　　专家认为，中国器官移植对死囚器官的长期依赖，不仅容易形成对死囚应有权利的漠视，也在很大程度上造成当下器官移植的尴尬局面：一方面是很多患者苦苦等待移植器官，另一方面是那些手握权力或金钱的人利用器官来换取利益。<br />
　　世界医学会秘书长奥特马・克罗伯尔（Otmar Kloiber）博士对《财经》记者说，该会向中国卫生部门呼吁过，应该停止使用死囚器官，“因为这在伦理上是不能接受的”。<br />
　　中国针对摘取死囚器官也做出了一些规定。1984年10月，最高人民法院、最高人民检察院、公安部、司法部、卫生部和民政部联合发布《关于利用死刑罪犯尸体和尸体器官的暂行规定》明确要求，无人收殓或家属拒绝收殓的、死刑罪犯自愿、死刑犯家属同意利用的，其尸体器官才可供利用。<br />
　　不过，一些地方在法规执行上存在着漏洞。在黄洁夫看来，解决这一问题的最好方式，就是建立器官捐献体系，摆脱对死囚犯器官的依赖。<br />
　　陈忠华认为，利用死囚器官就如同吸食毒品，一旦上了瘾，就难以戒掉。在过渡时期，利用死囚器官是权宜之计。但他同时建议，应制定《过渡时期的死刑犯人器官管理条例》，让这一过程公开透明，并且真正实现知情同意。<br />
　　值得注意的一个现象是，2007年最高人民法院收回了死刑核准权，每年被处死的犯人大大减少。与此相对应，中国器官移植数量在2005年、2006年达到高峰，此后两年则有所下降。以中国肝移植注册系统公布的数据为例，全国2005年、2006年实施肝移植2970例、2781例，2007年骤降至1822例，2008年也仅恢复到2209例。<br />
　　卫生部副部长黄洁夫2008年曾在医学杂志《柳叶刀》撰文披露，捐献者书面同意和死刑复核等措施，使得尸体器官移植数量减少了三分之一至二分之一。<br />
　　可以说，中国法治建设的进步、中国人权意识的不断提高以及来自国际的压力，使得死囚器官来源大大减少。但是，伴随着经济繁荣和小康社会渐成，社会对器官移植的需求愈发旺盛难抑。正因为不能继续依赖死囚器官，患者和卫生部门将目光投向了活体器官捐献。<br />
<br />
活体移植两面<br />
人伦生活具有太多的医学科技不可推知的复杂性。活体移植的推动者陈忠华开始意识到活体移植带来的不良后果<br />
　　“活体移植”是指自身健康的个人将器官移植给受者，让这个器官继续发挥功能，从而使接受捐赠者获得新生。<br />
　　“活体移植”无疑属于医学的进步。世界上第一例患者在术后长期存活的肾移植，就属于活体移植。1954年圣诞节之前，美国波士顿的约瑟夫・莫雷（Joseph Murray）医生，为同卵双生的赫里克兄弟施行了肾移植手术。哥哥罗纳德赫里克捐出他的一个肾，植入生命垂危的理查德赫里克体内。罗纳德的爱心和莫雷的仁术，帮助理查德多活了八年，直至31岁。罗纳德一直健康地生活着，医生莫雷则在后来获得了诺贝尔奖。<br />
　　陈忠华是中国活体移植手术最早的推动者之一。2000年，他从英国剑桥大学回国时，带回一本英国器官移植学会的《活体器官移植指南》，在不同学术会议场合公开介绍这一新技术。他还提出解决器官来源的两个策略：家庭内自救和多家庭互救――所谓家庭内自救，就是家庭成员为患者捐出器官；多家庭互救，就是不同的家庭之间“交叉换器官”。<br />
　　不过，那几年获得死刑犯的器官并不困难，且由于活体移植手术风险大，并没有太多的人和医院尝试活体移植。<br />
　　变化发生在2007年最高人民法院收回死刑核准权之后。当年，全国肝移植中，有24.7％来源于活体；而三年前，这一比例仅为0.32％。<br />
　　那之后，活体移植在中国大行其道。很多专家向社会公开推荐活体移植的“优越性”，诸如“活体器官移植的移植物的存活率要明显高于尸体供者，在存活率上有显著优势”；“活体移植的患者在移植后六个月的急排反应发生率明显低于尸体供者，来自亲属间的活体移植成功率更高”，“活体移植术后排异少，花费的医疗费用较低，是最可能实现的而且能够增加供体数量的有效方法”；等等。<br />
　　在大众传媒上，关于“活体捐赠”的报道也比比皆是。妹妹为兄长捐肾、母亲为女儿捐肝等等，媒体报道动辄冠以标题“彰显亲情”“义薄云天”……<br />
　　有些专家也在媒体上介绍说，“肝具有很强的再生能力”，“每个人都有两个肾，正常情况下捐出一个不会影响身体机能”，因此提倡“亲情捐献”。在这种舆论氛围下，亲属间如果不愿捐出器官，甚至有可能被贴上“不道德”的标签。<br />
　　然而，对活体移植的非议一开始就不绝于耳。关于活体器官移植，一直隐含着一个伦理争论――是否应该以伤害生命的方式拯救另一个生命。<br />
　　根据国际公认的生命价值原则，在活体器官移植中，不仅要尊重供体勇于奉献的高尚道德，还要求考虑受体手术后的生存时限以及生活质量，否则，这样的移植就不值得提倡。<br />
　　北京协和医学院生命伦理学研究中心翟晓梅教授接受媒体采访时强调，在伦理学上，不允许因为挽救一个人而牺牲另外一个活人。必须依照公认的医学科学标准，进行综合的利益与风险评估。只有在利益远大于风险，并且捐献者完全自愿的情况下进行活体捐献器官，才是伦理学上可接受的。<br />
　　毕竟，活体器官捐赠，多少会对捐赠者的身体造成伤害。有研究显示，美国有1％的肾移植供者发生终末期肾病。华中科技大学同济医学院朱兰等人在一篇论文中称，有些健康人在供肾以后，“自己却进入了等待肾移植的队伍”。<br />
　　活体肝移植的风险更大。有研究称，肝移植供者术后死亡率达0.2％-0.4％。<br />
　　“割掉一个肾，说没有影响，那是假的。”卫生部副部长黄洁夫告诉《财经》记者，“活体捐献还有并发症，肝移植活体移植中多达三分之一有并发症。我们已经出现了供体、受体双死亡的事情。”<br />
　　黄洁夫认为，活体器官移植应该属于不得已而为之。按照世界上公认的伦理学准则，移植器官首先应该来自脑死亡和心脏死亡的尸体器官，然后才是亲属和非亲属的活体器官。<br />
　　活体移植手术最早的推动者陈忠华教授也逐渐认识到，活体移植其实并不是一件应该鼓励的事。他曾经对媒体讲述过一个故事：他接触过一个家庭，弟弟是尿毒症患者，而哥哥早年因为烧伤注射链霉素，导致耳聋。父母决定，要残废哥哥把肾捐给弟弟。一家人都同意了，包括决定捐肾的哥哥。但陈忠华最终拒绝做这个手术。<br />
　　“残疾人没法知情同意，他本来就是一个弱势群体，我们怎么忍心再割掉他一个肾脏？这违反伦理学的原则。”他说。<br />
　　人伦生活具有太多的医学科技不可推知的复杂性。一系列问题相继出现，陈忠华开始意识到活体移植带来的不良后果。近两年，除非父母给子女捐献器官，陈忠华个人已不再做活体移植手术。<br />
　　“如果是子女给父母捐献肝或肾，我不但不做，还反对。”在陈忠华看来，年轻人面临更长的生命、时间、机遇的挑战，健康对他们来说是最重要的。<br />
