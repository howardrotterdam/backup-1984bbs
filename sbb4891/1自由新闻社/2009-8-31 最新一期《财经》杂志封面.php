													<h2>本文见《财经》杂志 2009年第18期 出版日期2009年08月31日</h2>
						
						
																											<div id="postmessage_221867" class="t_msgfont">《财经》记者 刘京京 李虎军 贺信<br />
<br />
一起在坊间流传已久、骇人听闻的“杀人盗器官”案，再次把中国器官移植事业推上了风口浪尖。<br />
　　8月25日至26日，全国人体器官捐献工作会议在上海举行。<br />
　　会议的主要议题，是建立一个由中国红十字会主导的人体器官捐献体系。全国各地的器官移植临床专家、卫生行政部门官员及红十字会代表等130多人云集一堂。中国在开展器官移植以来，如此规模的会议还是第一次――2006年11月14日于广州召开的全国人体器官移植技术临床应用管理峰会，确立了中国器官移植技术应用的六大准则，意义重大，但参会者主要为医务人员。<br />
　　会上，诸多与会者在私下里屡屡谈及最近发生的一起人体器官盗卖案――贵州某地发现了一具流浪汉尸体，其器官被摘取一空，用于器官移植。<br />
　　这个消息最早出现在器官移植领域颇有影响的“中国器官移植网”上，《环球时报》英文版继之报道了此事。报道称，广州的中山大学第三附属医院有医生前往贵州，与当地器官贩子接触，取得了流浪汉尸体的器官。<br />
　　在会议结束后的新闻发布会上，中央保健委员会副主任、卫生部副部长黄洁夫在回答《财经》记者提问时表示，虽然他不能代表卫生部证实这则传闻，但他个人相信，绝大多数记者是负责任且有良知的，不会无中生有。卫生部会根据公安部门的办案结果，以法律为准绳，以事实为依据，对此事进行处理。<br />
　　这起骇人听闻的“杀人盗器官”案，折射出中国器官移植的困境。<br />
　　中国的器官移植实践始于20世纪60年代。改革开放后，经济发展推动了医疗进步，人们对高端医疗服务的需求不断增加。1993年，美国的肝移植率（每百万人口的肝移植数量）是中国的5340倍；到2007年，这一差距迅速缩小到19.4倍，在此期间中国的年肝移植数量增长了400多倍。截至2008年，中国肾移植累计86800例，肝脏移植累计14643例。如今，中国已经成为临床手术数量仅次于美国的器官移植第二大国。<br />
　　由于缺乏可供移植的人体器官，能够接受移植手术的患者仍然是少数。在中国，每年有约100万患者需要肾移植，约30万人终末期肝病患者需要肝移植，但每年全国能开展的移植手术不过约1万例。用黄洁夫的话说，“只有1％左右的人能实现移植的愿望”。<br />
　　巨大的供需缺口催生了器官买卖的“地下市场”，促使一些逐利者铤而走险，甚至有部分医生参与器官移植灰色交易。<br />
　　面对器官移植领域的混乱，从2008年至今，一场规范器官移植的“整风”运动在全国范围内推开。<br />
　　此前，包括中山大学附属第三医院在内，中国共有164家医院获准提供人体器官移植服务。今年6月11日，卫生部下发通知，要求各地卫生厅局开展人体器官移植专项检查，重点检查164家移植医院开展活体器官移植情况和非器官移植医院是否违规开展人体器官移植。<br />
　　8月24日，卫生部人体器官移植技术临床应用委员会（OTC）召开第六次会议，通过了人体器官移植医院的资格复审结果。复审结果将很快向社会公布。卫生部副部长黄洁夫表示，医院和医生如果有违法违规行为，医院的器官移植资格会被取缔，医生也会被吊销执照。<br />
　　不过，仅规范医院和医生的行为，专注于器官移植的临床应用，并不能治本。一个共识是：器官移植发展的瓶颈是器官紧缺，解决之道就是在法治框架下建立起自愿捐献、公正分配的良性捐献体系。没有这样的捐献体系相配套，中国器官移植事业无法健康发展。<br />
　　也正因此，黄洁夫在这次器官捐献工作会议上坦言：“这次会议关系到中国器官移植事业的生死存亡。”<br />
<br />
涉足空白地带<br />
中国第一部关于器官移植的行政法规，令人遗憾地没有详述最为紧迫和复杂的器官捐献与分配问题<br />
　　器官捐献体系的建立，意味着一次多方位的改革。<br />
　　根据发达国家的经验，一个完整的器官移植体系，应该包括国家器官移植管理体系、人体器官捐献者登记系统、人体器官捐献和分配网络体系、器官移植临床服务体系、人体器官移植科学登记系统五个部分。其中，器官捐献体系不可或缺。<br />
　　在中国，器官捐献体系还是一个空白地带。<br />
　　2007年5月，中国第一部关于器官移植的行政法规《人体器官移植条例》（下称《条例》）开始施行。《条例》规定，由卫生部负责全国人体器官移植监督管理工作；此前已认定的164家移植医院，构成器官移植临床服务体系；组建负责收集器官受者信息并进行分析评估的科学登记系统――今日看来，对于中国的器官移植事业，《条例》的颁行无疑是一个里程碑式事件。<br />
　　但是，《条例》并未能详述最为紧迫和复杂的器官捐献与分配问题，只在“总则”中提出各级红十字会依法参与人体器官捐献的宣传等工作，未涉及捐献体系如何建立、如何操作等具体事项。<br />
　　捐献体系的缺位，在《条例》制定时就埋下了种子。中华医学会器官移植学会副主任委员、华中科技大学同济医学院器官移植研究所教授陈忠华认为，《条例》的制定有相当大的国际压力，过程较为匆忙，难免有不足之处。<br />
　　在《条例》出台后的一段时间里，卫生部门将精力集中于164家移植医院资格的认定、肝肾移植数据中心的建立，以及脑死亡标准、立法问题的讨论，捐献体系的建立则被搁置。<br />
　　在现实中，红十字会一直在开展遗体捐献工作。不过用红十字会总会常务副会长江亦曼的话说，“还存在一定局限性”。例如，遗体捐献主要用于医学教学和医学科研，用于器官移植的比较少；捐献者以老年人居多。<br />
　　更为技术性的因素是，在很多医务人员看来，用于医学教学的遗体捐献不需要太高的技术含量，器官捐献则不同，需要判断哪些人的器官适合捐献，器官的摘取也有时间等严格要求。这不是单靠红十字会就能完成的。<br />
　　在广州律师宋儒亮看来，离开医疗机构和医护人员，器官供体配型、捐献、摘除、运输、保存、使用等程序难以进行，而且连最根本的器官供体也难以找到。<br />
　　如今，器官移植供体紧缺形势的恶化，终于催促卫生部门和红十字会联手创建捐献、分配体系。<br />
　　8月26日，在全国人体器官捐献工作会议总结会上，中国红十字会总会和卫生部宣布，将着手建立人体器官捐献体系。黄洁夫表示，2009年下半年试点省市应开始准备；2010年，试点将正式启动。如果一切顺利，捐献系统有望在2011年开始向全国铺开。<br />
