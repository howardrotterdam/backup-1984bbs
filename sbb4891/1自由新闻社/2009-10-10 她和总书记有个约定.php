重庆晚报12月10日报道 “我毕业后会回家乡支教!”今年5月，中国农业大学学生庹蓝兰当着所有师生的面，跟胡锦涛总书记约定。言出必行，庹蓝兰毕业后放弃高薪和爱情到丰都支教，当了一名乡镇中学教师。<br />
<br />
22岁的庹蓝兰是重庆彭水县人。2002年，吃百家饭的她上了重庆首届南开中学宏志班，随后考上了中国农业大学。2009年5月2日，胡总书记来到中国农业大学，同师生共迎五四青年节，庹蓝兰作为农大唯一一名应届毕业生代表发言。<br />
<br />
5分钟的发言，庹蓝兰讲述了母亲去世、父亲再婚，她独自带着3个妹妹，在每个任课老师家轮流住宿，最后考进重庆首届宏志班的往事。最后，她当着所有老师和同学的面跟总书记说：“我毕业后会回家乡支教!”会后，总书记握着她的手，表扬她和其他同学主动到基层工作的精神。<br />
<br />
庹蓝兰说，与总书记的约定并非一时心血来潮。读大三时，她和高中同学一起到丰都农村小学义务支教，目睹了当地落后的教育现状，决定毕业后到那里。今年1月，庹蓝兰瞒着老师和同学，参加了丰都县特岗教师考试，获得执教两年的资格。<br />
<br />
听说她要去丰都支教，很多同学都说庹蓝兰是疯子。当时，她刚接到两份北京企业的录用通知，其中一个企业开出的月薪在8000元以上。男友听说庹蓝兰要回去支教，和她分手，这让庹蓝兰一度非常伤心。<br />
<br />
如今，庹蓝兰在丰都龙河镇第一中学当物理特岗教师，月薪1500元左右。为了供3个正在念初中的妹妹，庹蓝兰自己买菜做饭、租住月租100多元的农房：“日子过得虽紧张，但我会尽力安排和照顾好家人”。<br />
