<br />
向总书记承诺回乡支教<br />
<br />
南开中学首届宏志班班主任乔正蓉说：“全班学生来自重庆市各个区县的贫困家庭。”2005年，重庆首届宏志班参加高考，南开中学宏志班除一名学生因身体素质差没考上外，其他的学生全部考上了重点本科。如今这群当年被媒体广泛宣传的特殊学生已经毕业，首批50名宏志班学生中，有23名同学选择在北大、清华等名牌高校里读研或者硕博连读，25人在北京、成都、上海、南京等大城市参加工作。<br />
<br />
今年5月2日，胡锦涛总书记来到中国农业大学，同广大师生共迎五四青年节，重庆彭水女孩庹蓝兰作为中国农业大学唯一一名应届毕业生代表发言。<br />
<br />
庹蓝兰说，那天在学校生命科学大楼的一个会议室里，她在5分钟的发言时间里，讲述了小学时母亲去世，父亲重组家庭后，独自带着3个妹妹的她靠在每个任课老师家轮流住宿，最后考进重庆首届宏志班的往事。这个来自重庆彭水的女孩当着所有老师和同学的面跟总书记说：“我毕业后会回家乡支教！”<br />
<br />
“总书记紧紧握着我的手，表扬我和大家能到基层去工作。”庹蓝兰说，“其实，与总书记的这一约定，并不是当时突然想到的。在大三的时候，我和高中同学一起到丰都一个农村小学义务支教的时候，看了那里落后的教育现状，我就决定毕业后还要到那里。今年1月，我就瞒着老师和同学，参加了丰都县特岗教师的考试并获得了执教两年的资格。”<br />
<br />
不后悔放弃高薪和爱情<br />
<br />
“听说我要去丰都支教，很多大学同学都说我是疯子。”庹蓝兰说，当时，她刚刚接到了两份企业的录用通知。“两个工作都在北京，待遇好的那个给我开出的月工资在8000元以上。”她说。<br />
<br />
“他听说我要回去支教，毫不犹豫地和我分手了。”提起大学男友因为支教跟她分手的事，小庹说，“当时很伤心，但是为了理想，我并不后悔。”<br />
<br />
还要养活3个妹妹<br />
<br />
现在的庹蓝兰在丰都龙河镇第一中学当物理特岗教师。“每天早上8点钟之前起床，一周23个课时，下课后去路边的农户那里买菜自己做饭，这样能节约下一笔钱。”庹蓝兰平静地说起她现在的支教生活，“我租了一间农家的房子，月租金100多块钱，要节约些。”<br />
<br />
当听说记者要拍照时，女孩从家中拿出了一件黑外套说：“这件黑外套是我最好的衣服了，呵呵。”<br />
<br />
“日子过得很紧张，但是我会尽力安排生活和照顾我的家人。”女孩说，当特岗教师每个月只有1500元左右的收入，除了日常生活外还要供3个正在念初中的妹妹上学。在丰都家中，谈起姐姐的选择，她的3个妹妹集体抗议：“我们想姐姐能到北京大城市工作！”<br />
<br />
“后悔不？可能你再也回不到北京工作了。”记者问。庹蓝兰说：“我觉得现在的价值观很多元，来家乡支教是我内心的渴望！”（人民网-人民日报海外版）<br />
