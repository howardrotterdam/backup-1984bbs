													<h2>留学生为“平反六四”拉横幅飞越纽约中领馆</h2>
						
						
																											<div id="postmessage_657654" class="t_msgfont"><a href="https://yopic.us/?v=boxunsign0.jpg" target="_blank"><img src="https://yopic.us/thumbs/boxunsign0.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /></a><br />
巨型横幅全文<br />
<br />
海外网络广泛流传视频，一组80后、90后中国留学生在六四事件21周年当天，驾驶飞机拉横幅飞越中国驻纽约领事馆上空，要求昭雪六四，结束专政。<br />
<br />
2010年6月4日下午，一架飞机拖着写有28个汉字的巨型横幅在纽约哈德逊河上空低空飞行，横幅写道：“昭雪六四，结束专制，人民必胜，良知永存！河蟹，草泥马喊你从中国滚蛋！”<br />
<br />
这是一群中国留学生为纪念六四事件21周年策划的行动。三个架次的飞机低空在中国驻纽约领事馆和自由女神像之间蔚蓝的天空上盘旋。<br />
<br />
这次行动的总指挥是1984年出生的哥伦比亚大学毕业生孔灵犀。他在接受美国之音采访时说，六四事件是他人生的第一个连贯的清晰记忆，他从来没有忘记六四时学生上街游行的景象。<br />
<br />
*头脑风暴产生纪念六四新创意*<br />
<br />
<a href="https://yopic.us/?v=boxunkongl.jpg" target="_blank"><img src="https://yopic.us/thumbs/boxunkongl.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /></a>行动总指挥孔灵犀(右)与王军涛<br />
孔灵犀说，今年5月中旬他在一个会议上与中国留学生施毅不期而遇，他对施毅谈起他的一项人力直升机的发明创造，并说希望能够在六四事件21周年当天，驾驶他的发明，拉着纪念六四的横幅在曼哈顿大街上穿过。<br />
<br />
89年出生的施毅到美国求学期间学习了飞机驾驶。孔灵犀说，施毅把他的创意进行了升级。他说：“他作为一个飞行员，他完全可以搞一架飞机，申请许可，然后在天空拉横幅，他说，这是商业行为。他说，很多商家也这样做，来打广告等等，所以他说，这是完全可行的。然后我们就和其他的朋友商量，大家都觉得这是一个很好的办法。”<br />
<br />
当时离六四还有十来天的时间。孔灵犀说，他们马上付诸行动，筹集捐款、申请许可、租赁飞机，进行试飞等等。孔灵犀说，行动的预算是2000美元，但因为要制作的横幅是汉字，每个汉字长、宽约1．5米，制作费用比英文字贵一些，最后使总费用达4000多美元。<br />
<br />
*百年学子，共同梦想*<br />
<br />
孔灵犀说，直接参加这次行动的中国留学生有十多个人，最后乘飞机上天的有四人。他说，作为中国留学生，他跟百年来出国学子怀着的是一个共同理想。他说：“到了另外一个国家，你就更思念自己的祖国，看到祖国这个样子，看到美国这么好，希望能够改变这些现状，给文化、社会等等找一个出路，找一个答案。我相信，这是每一个学者，100多年前胡适他们也都是这样想的。”<br />
<br />
*王军涛：薪火相传，后继有人*<br />
<br />
<a href="https://yopic.us/?v=boxuncapit.jpg" target="_blank"><img src="https://yopic.us/thumbs/boxuncapit.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /></a><br />
机长施毅与王军涛<br />
哥伦比亚大学政治学博士王军涛应邀与年轻一代的留学生们飞上了天空，纪念六四。他对美国之音说：“我自己在天上飞时也很激动，当我看到下面曼哈顿的大楼，飞过领馆的上空，看到80后和90后扯出那张旗子，就是关于六四，给予勿忘良知，还有用80后和90后特有的愤怒语言说河蟹和草泥马时，心里感到特别的畅快。”<br />
<br />
当年的天安门学生民主运动领袖王军涛说，他从参加这次行动中看到了薪火相传、后继有人。他说，由于近两年来中国政治与社会矛盾日益激化，越来越多的年青人开始重新思考历史。当他们得知六四屠杀的真相时，包括邀请他飞行的80后、90后，和他的母校北京大学在校学生都告诉他，他们所感到的震撼与悲痛。<br />
<br />
王军涛表示，他毫不犹豫地接受了年轻一代留学生飞越纽约上空纪念六四事件的邀请，他说，这是新一代年青人以他们的创意，他们的方式追寻历史，探索正义。<br />
<br />
*周舵：80后、90后良知与正义感的觉醒*<br />
<br />
在1989年民主运动中被称为“天安门四君子”之一的周舵说，中国留学生的行动令他感到鼓舞。他认为，这标志着中国年轻一代的觉醒，他一直相信，一旦中国的年青人冲破新闻封锁、信息封锁，得知六四真相，决不会善罢甘休。他说：“只要是被下一代得知真相，我相信绝大多数人都是有良知，有正义感的。他们的政治冷漠很快就是转变。”<br />
<br />
周舵为纪念六四事件21周年刚刚撰写了题为“不可持续的拖字诀治国”的文章。他说，中国留学生在纽约采取的行动证实了他的观点。他说：“21年了，中国政府采取的主要策略一个是封锁消息，不让年青一代知道六四，所以很多80后、90后根本就不知道有六四这回事。另外第二个策略就是让整个社会庸俗化，鼓励每个人只关心自己的私利，只看自己脚下，不要关心什么国家，天下，把每个人都变成精神上的侏儒。”<br />
<br />
*策划人：无愧于子孙后代*<br />
<br />
这次行动的策划者兼飞行员施毅对博讯网站的记者表达了他拒绝谎言、拒绝沉默的立场。施毅说：“当我们进入暮年，我们的子孙后代问起我们在社会秩序、道德与文化全面重建的过程中都做了些什么时，我会告诉他，我没有保持那份可耻的沉默，是我为国家带来了积极的变革。”<br />
