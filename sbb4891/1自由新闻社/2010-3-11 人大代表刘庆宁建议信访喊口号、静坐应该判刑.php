　　人民网北京1月7日电　全国人大常委会副委员长、民建中央主席陈昌智今天上午做客人民网强国论坛。有的网友说，“不想当执政党的政党不是好政党”，对此，陈昌智结合我国的具体国情和建国数十年的发展成果做了反驳，并认为“不接受共产党领导的参政党不是一个好的参政党。”<br />
<br />
　　陈昌智表示，由中国共产党领导的多党合作和政治协商制度肯定是符合中国国情的。他认为，有的网友提到的“不想当执政党的政党不是好政党”这句话不适合中国国情。陈昌智说，世界上的政党制度可以分为三种：一种是两党或者多党竞争的竞选、轮流执政的制度；第二种就是一党执政；第三种就是中国这一种――一党执政，多党合作。“不想当执政党的政党不是好政党”这句话只是放在实行第一种政党制度的国家才是合适的。<br />
<br />
　　陈昌智并详细阐述了中国为什么要实行由中国共产党领导的多党合作和政治协商制度。他说，一个国家的政党制度是由这个国家的历史、国情、国家性质等等决定的。民建在1945年成立时，是为了反对国民党内战、反对独裁、反对破坏，要民主、要建设。而这和共产党的宗旨也是一致的。到新中国成立以后，共产党也很好地领导了这个国家，克服了自身的缺点和问题，特别是改革开放30年来，共产党领导国家、领导全国人民，逐步走上了民族复兴的大道。我们的国际地位迅速提高，人民生活水平得到迅速提高，经济有了很快的发展。特别是2008年，我们遇到这么多难事、大事，我们都解决的很好，办得很成功。这充分体现了中国共产党的领导使得中国的国际地位不断提升，经济不断发展，人民生活水平不断提高，所以我们执行这种制度是符合我国国情的。陈昌智强调，我们政党制度的特点就是共产党领导、多党派合作，共产党执政，多党派参政，把这句话转过来，应该说不接受共产党领导的参政党不是一个好的参政党，不为国家的经济发展、不为人民群众生活的改善出力，或者你的参政议政工作做得不好，这就不是一个好的参政党。<br />
<br />
