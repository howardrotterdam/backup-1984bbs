新民网3月11日报道 “信访是宪法赋予公民的政治权利”，全国人大代表、广西人大法制委员会副主任、民建广西副主委刘庆宁在提交的一份关于信访的议案中第一句就这么写到。不过，他认为到领导办公室和生活地点闹访、不分场所找领导闹访等信访问题很严重，已经“严重影响领导正常生活秩序和正常工作秩序”，所以提交议案，希望修改《刑法》增设“扰乱信访秩序罪”。<br />
<br />
他列出了20种应受到刑罚的信访行为，其中包括信访时喊口号、打横幅、散发材料、静坐，未经批准在国家机关办公场所及其周边地区非法聚集、滞留，信访时采取自伤、自残、自杀、跳楼等行为……这些行为如果多次或有严重后果，那么将依其严重程度，处以3年以下、3年到7年、7年到15年有期徒刑。<br />
<br />
昨晚南都两会“说吧”照例在微博互动平台发布今日征求网友意见题目，引发热议，一个多小时评论超过150条，大多数网友都对这一议案表达了愤慨，认为老百姓日子过不下去了才上访，却因为影响领导生活秩序而要坐牢，这无论如何说不过去，多名网友指出这一议案已经明显违背《宪法》，“请这位代表重新，或者第一次也行，读一遍宪法。”王小山评论。<br />
<br />
信访影响领导生活、工作<br />
<br />
议案中提出，不依法信访问题十分严重，原因一是应解决的多年未解决，二是对法律法规及政策理解不同，三是无理信访给国家机关施压，四是无理信访给地方施压。“有极少数人知道党中央及国务院高度重视信访工作，为追求利益最大化，主动进京信访给地方施压。”<br />
<br />
在他看来，当前不遵守信访秩序，串联信访，收钱代访，到领导办公室及公共场所和领导生活地点闹访，不分场所找领导闹访，特别是县级及以下地方，不分场所找领导闹访问题十分突出，“严重影响领导正常生活秩序和正常工作秩序”。同时，《治安处罚法》对年龄大和未成年人不能治安处罚的规定，加剧了信访秩序的混乱。<br />
<br />
为此，刘庆宁认为有必要依法规范全国的信访秩序。<br />
<br />
多次静坐?判3年<br />
<br />
他提出的方案是修改《刑法》，增设扰乱信访秩序罪。他在方案中列出了20种信访行为应受到刑罚：<br />
<br />
有其中之一的，处3年以下有期徒刑、拘役;情节严重的处3年以上7年以下有期徒刑;情节特别严重的，处7年以上15年以下有期徒刑。<br />
<br />
这20种可能导致坐牢的行为包括：到禁止信访地方走访的多次或有严重后果的(以下各项均有“多次或有严重后果的”定语，从略);未经批准在国家机关办公场所及其周边地区、广场等重要场所或重大活动期间的主要场所，非法聚集、滞留、围堵出入口、游行、示威或者其他扰乱公共秩序的;信访时采取呼喊口号、打横幅、穿状衣、出示状纸、散发上访材料、静坐等方式扰乱公共秩序的;滞留、占据信访接待场所;以信访为名，阻挠干扰企事业单位生产、工作、教学、科研等正常活动的;信访时采取自伤、自残、自杀、跳楼，摆放尸体、骨灰盒等容易造成公众心理恐慌的手段相要挟的;信访时无理取闹、扰乱工作秩序，拒绝、阻碍国家机关工作人员依法执行职务或者强行冲闯公安机关设置的警戒带、警戒区的;不依法在指定地点上访的;信访听证终结上访，又违反信访法规秩序的……<br />
