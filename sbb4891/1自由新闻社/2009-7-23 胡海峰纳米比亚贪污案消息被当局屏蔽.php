・生意第一，公理靠边<br />
<br />
“中国人正在经历其他国家多年前正在经历的阶段，而他们意识到了自己的不足，”布劳蒂干女士说，“中国人不会特别希望弄出什么大丑闻。这和他们的利益冲突。他们只是希望自己的公司弄到生意。”<br />
<br />
有时他们并不能如愿。2007年菲律宾政府曾因舆论压力不得不中止了一份与北京某扫描器生产厂商签订的价值460,000,000美元的合同。批评家认为该公司根本没有完成这个项目的能力。（事实上这个被拒绝的企业跟此次遭纳国调查贿赂弊案的企业是同一家，清华同方威[不能说]视技术股份有限公司。）<br />
<br />
同样因大宗回扣问题曝露被废除的还有一份与中兴通信的价值329,000,000美元的合同，中兴是另一家中国国有通信运营企业。中兴否认曾提供任何贿赂，但质疑之声一直盛嚣尘上。上月反渎职小组刚提交了关于针就此合同的两名被控收贿的菲方官员的犯罪调查报告。<br />
<br />
一个马尼拉的非盈利性组织，国际法律中心已经准备好对另一个中方项目的控告，该项目设计一份高达500,000,000美金的地铁建设合同。担任该中心主任的洛克教授把中国国有企业的报价称为“贵得不靠谱”。中国官方对其官控公司的指示只是在海外不要踩过公理底线。（国内没关系？XD）<br />
<br />
“我们的企业作生意时应该遵循国际规范，必须公开化透明化，大型建设项目必须要通过招标竞争，严禁不恰当密约，杜绝腐败和回扣，”中国总理温[影帝]家宝2006年在赞比亚对中国的生意人曾经这么说。但中国根本没有什么特别的法律针对跨国官员贿赂案件。当官府嫡选的公司在国外因进行贿赂操作闹出丑闻被驱逐后，中国政府也根本不会急于调查。反而会急于寻找其他途径开拓市场，因为它手中握有很多海外合同――其中一些甚至与中共领导自己的家族挂钩。<br />
<br />
譬如，一月份，世界银行禁止四家中国国有企业参与竞标某菲律宾银行项目，因为调查显示这些企业私下联合起来提高报价。而直到现在，丑闻中的其中两家单位在中国商务部的准核海外援助企业名单上依然在列。<br />
<br />
此次的纳[敏]米[感]比[国]亚弊案之所以犹为敏感，是因为胡[和谐]锦[折腾]涛的大公子胡[太]海[子]峰直到去年还是涉案公司（清华同方威[不能说]视技术股份有限公司）总裁。现在这位胡[太]海[子]峰担任着共产党驻包括威[不能说]视和多家公司在内的母公司的书记一职。而在今夏纳国弊案浮出水面之际，中共中央政府立刻作出的反应是，从媒体到网络全面封锁一切丑闻相关信息。<br />
“这对任何人都是一个倒退的信号，”驻北京的中国政治观察家拉塞尔.利.莫斯说，“所有一切回到了最初状态，因为当你看到一个球滚动时，没人直到它会在哪里停下来。谁也不想掉饭碗。”<br />
<br />
威[不能说]视否认了温得和克文件中所列关于它的所有不当操作的指控。一位发言人声称公司不会对此作出评论，因为事件尚未结案。中国商务部和其他政府部门也一起保持了沉默。<br />
<br />
纳[敏]米[感]比[国]亚反腐调查组声称威[不能说]视向一家由纳[敏]米[感]比[国]亚某官员建立的公司提供了高达420万美金的回扣，而这份丰厚贿金被该官员，他的生意伙伴，以及威[不能说]视驻南非代表――一个中国人划块分享。<br />
<br />
・终止於逮捕的密约<br />
<br />
中国将威[不能说]视公司表扬为其在全球市场的“常胜将军”。在10年时间里，该公司将其客户拓展到全球60多个国家，其主营为检查走私危险品的高科技货舱扫描器，威[不能说]视的发言人声称该公司是全国唯一一家能生产此类产品的企业。<br />
<br />
纳[敏]米[感]比[国]亚政府则致力於加强对其在国际贸易中的机场，海关，边境的走私危险品检查。在2005年的一次对中国的国事访问中，纳[敏]米[感]比[国]亚总统Hifikepunye Pohamba访问了威[不能说]视总部及其工厂，根据法庭文件，在第二年，威[不能说]视就派驻了杨[敏感人物]帆作为其驻纳[敏]米[感]比[国]亚首都温得和克的代表。<br />
<br />
几个月后胡[和谐]锦[折腾]涛访问温得和克，开启了对纳国的金援项目。“当中国说只有天空是有限的，其实他在暗示把任何你想要的说出来，”同样接受调查的纳[敏]米[感]比[国]亚财政部部长卡尔.斯凯勒特文说。<br />
<br />
起初，斯凯勒特文先生说，起初谈判一度停顿，因为纳[敏]米[感]比[国]亚方面并不想同意中国在它的矿产资源上分一杯羹，以换取最高限额贷款。之后中国放弃了该条件，而纳[敏]米[感]比[国]亚得到了一份价值100,000,000美元，利率2.5％为期20年的贷款。“仅从财政观点来看，这是一笔好买卖。”斯凯勒特文先生认为。<br />
<br />
纳[敏]米[感]比[国]亚政府决定用把这笔贷款主要开销在货舱扫描器上。斯凯勒特文先生也参与了扫描器合同的谈判，谈到他想在世界范围内的扫描器供货商发布招标时，中方谈判人员拒绝了这一提案。<br />
“他们说‘那并非我们的系统，我们会告诉你该从谁那里去买这些设备。’我们所有人，包括总理在内都对这种不透明的作法很惊慌。”斯凯勒特文先生说，但中国政府给我们的答复是“不会耍我们的”。<br />
<br />
三月在纳国财务部支付威[不能说]视12,000,000美元预订金后，斯凯勒特文先生的不安转化成了一场危机。一位温得和克银行负责人对纳国对威[不能说]视这笔汇款提出质疑，诘难为何威[不能说]视在收款后将4,200,000美元存进了一家由纳[敏]米[感]比[国]亚公共服务委员会委员特克拉.拉梅克经营的顾问咨询公司的户头。<br />
斯凯勒特文先生声称从未见过拉梅克女士，而她的顾问咨询公司也与扫描器合同完全无关。这提醒了纳[敏]米[感]比[国]亚反腐办。今年7月，拉梅克女士和她的生意伙伴及威[不能说]视驻温得和克代表被控触犯纳国反腐法律均遭逮捕。被捕的三人都否认参与违法操作。<br />
<br />
・深入调查<br />
<br />
调查人员指出，威[不能说]视公司在2007年胡[和谐]锦[折腾]涛访问后仅一月就雇佣了拉梅克女士的顾问咨询公司特可贸易。威[不能说]视答应如果扫描器单价能达到250万美金一台，就支付特可公司合同10%的收益作为报偿。如果价格还能更高，威[不能说]视会把增加收益的五成分给特可。最后交易的价格令特可拿到了12,000,000美金的回扣。<br />
<br />
在上个月被保释后，威[不能说]视代表杨[敏感人物]帆说，他的公司之所以雇佣特可因为“特可向他解释了在纳[敏]米[感]比[国]亚应该怎么作生意”。他并未作进一步解释。但早在2007年。另一位纳[敏]米[感]比[国]亚官员向反腐办控告拉梅克女士向中国驻温得和克大使馆介绍自己为Swapo（西南非洲人民组织）代表，而Swapo正是纳[敏]米[感]比[国]亚的执政党。她声称在纳[敏]米[感]比[国]亚没有一件生意Swapo不涉手。<br />
<br />
调查人员寻求威[不能说]视公司对此事件的解释长达两个多月，但没有迹象表明该公司准备配合调查。尽管如此，调查人员依然对他们抱有期望。<br />
<br />
纳[敏]米[感]比[国]亚首席检察官马沙.伊毛瓦7月曾造访北京，希望对威[不能说]视高层和其他一个涉案公司进行分开询问。但根据她的代言人丹尼.斯毛所言，伊毛瓦只被允许向中国最高检察院国际处提问。<br />
<br />
调查期间，法院暂时冻结了威[不能说]视名下的12,000,000美金资金。同时，纳[敏]米[感]比[国]亚财政部长斯凯勒特文先生迟来的说明证实曾经有更好的供应商提供价格更适合的扫描器。当他向南非官员询问价格时，他被告知威[不能说]视的合同已被调查。<br />
<br />
也许只是预言，竞争者说纳[敏]米[感]比[国]亚答应支付的价格远比实际贵得多。威[不能说]视在美国的主要竞争对手Rapiscan的副总裁彼得.坎特认为该系统的合理估价应为28,000,000美元，比威[不能说]视合同价格低了25,000,000美元。<br />
<br />
斯凯勒特文先生上月试图通过官方渠道给威[不能说]视现任总裁荣泳霖写信，希望就合同重新进行谈判。但中国驻温得和克大使馆官员拒绝了此信件，并称他并不知道有这个人。<br />
<br />
