<br />
 <br />
<br />
在海外发展同样迅速的同方威视公司，在零七年就曾被南非的《邮卫报》质疑，为了赢得当地一个价值约26亿元人民币扫描合约涉嫌行贿国会议员。而该报报道指该公司在菲律宾，也卷入类似的贪污行贿漩涡。<br />
<br />
 <br />
<br />
有网上评论认为，此次Namibia案件披露出Hu Jintao出访时批出的对外贷款被用来购买其儿子公司的产品，这种操作有套取国库、洗钱的嫌疑。高瑜说即使果真如此，习惯大陆社会现状的民众也不会有太大的震惊。&quot;他这种运作方法现在海外有的就说属于洗钱。现在也都知道腐败无所不在，本来这个就是靠着腐败作为润滑剂运作的政权，这一点只不过是有了又一个具体的例子罢了。&quot;<br />
<br />
 <br />
<br />
在此之前，威视公司在中国大陆的发展早就受到质疑是与利用家族影响力有关。该公司零六年底拿下了奥运安保在大陆１４７个机场港口安装液态透视扫描仪的数十亿元合约而遭到外界的质疑，尽管当时中国民航总局称：&quot;获得合约由于威视的产品比同行优胜。不存在政治原因。&quot;但这种结论如何得出，政府招标采购程序有是否合乎规定等疑问最终没有答案，连合同内容都从未向公众公布。<br />
<br />
 <br />
<br />
深圳的评论人士朱建国认为，向下规定官员亲属不准经商的同时，国家领导人却不受约束的做法实难以服众。&quot;规定是有的，但到了最高层反而好像就没有限制了，有点法不上大夫了。国外不一定有这种规定，但作为中国特色，因为权钱交易官商勾结腐败程度太严重了，在这种特定时期，国家领导人应该做出牺牲，为了排除负面作用，应该禁止孩子、夫人、直系亲属参与任何商业活动，更不能在其中充当关键人物、领导人物了。但恰恰这一点我们国家一直没有做到。&quot;<br />
<br />
