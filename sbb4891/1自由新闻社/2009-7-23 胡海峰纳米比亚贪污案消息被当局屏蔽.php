中国有目的地加大对非洲支援力度<br />
[记者] SHARON LaFRANIERE and JOHN GROBLER<br />
09.21.2009<br />
<br />
温得和克，纳[敏]米[感]比[国]亚的首府――有国际性影响力地领导人很少会来到这个总人口不到两百万人的南非州国家踏上沙丘间的那些碎石路。所以当中国首席领导人胡[和谐]锦[折腾]涛在2007年带着130人的访问团来到这个荒凉的国家时，显然不会仅仅只是一次对黑非洲兄弟的礼节性拜访。<br />
<br />
事实上，中国之后很快就同意向纳[敏]米[感]比[国]亚提供一笔可观的低利息贷款，以帮助纳[敏]米[感]比[国]亚购买一套价值55,300,000美金的中国制造的货舱扫描器，用以检查走私物品。这是一桩单纯的生意，中国官方声称，它令中纳双方彼此互惠。<br />
<br />
事情看上去似乎的确如此，直到纳国需要向中国官方指定生产商――一家由胡[和谐]锦[折腾]涛的长公子经营的国有企业支付款项――包括帮助推动此项目的幕后感谢金――也就是我们常说的回扣。直到纳[敏]米[感]比[国]亚的调查人员彻查此事时发现中国人对检查设置重重障碍。<br />
<br />
於是人们发现扫描器事件似乎揭开了冰山一角，关于援助者的潜意，秘密合黑箱操作，中国利用亿万金援收买发展中国家的野望。<br />
<br />
从巴基斯坦，到安哥拉，到吉尔吉斯坦，中国正利用它世界第一的外汇储备加固它的外交联盟，攫取自然资源，同时为它的旗舰厂商招徕生意。对外援助――比较典型的如低率贷款，有时甚至捆绑了最高商业信用贷款限额。<br />
<br />
发展中国家的领导们拥抱了来自中国的更易获得的商业支援，因为西方国家的援助往往附带有对受援国政治经济改革的要求，这些要求都是发展中国家领导人们不想进行的。中国式新路线的结果已经明显显现出来了，发电厂，电子通信网络，2001年后在非洲大陆上有超过200个项目投入建设，大部分都由中国政府的进出口银行提供优惠贷款。<br />
<br />
专家们对中国金援目的谈论日益增加：所有金援都被要求从中国公司购买产品或服务，大部分为中国官员们自己挑选的特定的国有企业。被援国企业若参与投标定然被冷落一旁。而中国对资金支出，贷款期限，偿还条件等的一切都采取它惯有的不透明作法。甚至提供给受援国的贷款总额都被作为国家机密不对外透露。<br />
<br />
反腐人士指责保密措施滋生了贪污，而腐败侮辱了国际援助。<br />
<br />
“中国正用它的金钱购买外国政治精英阶层的忠诚，”菲律宾大学的法学教授哈里.洛克说，他对中国对菲律宾的金援提出质疑。“这是非常有效的一种软外交方针，但这对真正在为国际贷款付出代价的人民没有好处，反而使他们背上了沉重的债务。”<br />
<br />
而这种保密金援也与国际经济援助的宗旨背道而驰。尤其对那些领导层腐败无能的贫穷国家来说，到底这种中国式金援帮助了谁？按国际发展专家的说法，答案在大众心中，人们心知肚明。<br />
<br />
“我们对中国对外援助的所知甚至不及我们对他军备支出的了解，”乔治.华盛顿大学的中国专家兼作者沈大伟谈到，“这种（保密的）国际金援政策跟中国政府展现的对国际公理的遵从实在太矛盾了。人们会忍不住想，他们到底想掩饰点什么。”<br />
<br />
其实直到最近，发达国家也没有提供一个足堪标准的国际援助范本。许多项目都因为涉嫌贪腐，使倡议人而非贷款人受益而不得不叫停。但在过去10到15年时间里，大约30个发展中国家在O.E.C.D（经济合作组织）的保护伞下终于摆脱了金援债务阴影。<br />
<br />
人们希望外国援助的使用透明化，采取竞争投标并不为贿赂所扰。受援国在供应商和签约人之间有更大的选择余地，而不应坚持将资金回流回金援来源国的公司。<br />
<br />
中国并不是O.E.C.D的成员国，而它使用的金援操作方式已广为西方国家摒弃。因为这种金援把生意与援助捆绑起来，变味成了一种政府与政府之间的秘密和约。而在中国国内，这种国际援助的项目招标也非公开对全社会开放，而只是一种政府内定的关门选秀。种种作法给人们的感觉是，中国方面对其公司的境外腐败似乎无意管控。<br />
<br />
一些发展中国家坚持接受来自中国的慷慨必须建立在独立竞价的原则上，但也有不少对此无所谓。“通常情况下（如果他们无条件接受中国金援）相关人员会得到一些不可能在其他选择中得到的好处。”来自伦敦的中非关系专家克里斯.埃尔登坦言。“他们可能从中国人那里得到相应的酬款。”<br />
<br />
发展问题专家说他们已努力去说服中国政府，更好的质保和更开放的操作过程会给他们的援助获带来更佳的影响力和贸易机会。如果援助项目因为回扣和无谓的成本膨胀而迫停，中国可以停止商品和服务出口，但它的贪腐的名声也会就此成为铁案――在中国国内这已成为一个不得不面对的严重问题。<br />
<br />
然而即将出版的研究中非经济纽带关系的著作《龙的礼物》的作者德波拉.布劳蒂干认为，在中国公司具有和西方企业有竞争力前，北京方面必然会对采用西方的经济援助模式持犹豫态度。<br />
