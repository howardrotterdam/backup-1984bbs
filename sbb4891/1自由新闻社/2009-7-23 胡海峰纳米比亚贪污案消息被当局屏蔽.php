 <br />
<br />
威视公司在非洲Namibia涉嫌贪污行贿诈骗案件，原定本周三在纳国开庭。据《法新社》报道，被告律师以需要时间准备另一个关于申请调查期间资产和银行户口解冻的听证会为由，申请庭审延期，法庭周三宣布将审讯延迟至下周二及周三两天。<br />
<br />
 <br />
<br />
由于相关案件报道以及网络消息在中国大陆的传播被禁，因而有猜测指，此次审案推迟也是中共政府在发挥影响力。前北京《经济学周报》副主编高瑜说：&quot;Namibia推迟审判，当然这都有中国起的作用，这种属于重大的事情是他们首要工作，当然要把事件抹平，而且千万不能涉及胡公子。中国面对这样的问题面不改色，对外抹平，对内坚决不承认，其它的一些说法随着时间也就过去了。&quot;<br />
<br />
 <br />
<br />
香港城市大W政治W教授郑宇硕分析案件不会究责Hu Haifeng 更不会牵扯Hu Jintao，但必然令中共当局国内肃贪的可信度再打折扣。&quot;就算有这么一个案件，也不会实质牵扯到Hu Jintao或他的家庭成员，总是会有公司行政人员承担责任嘛！只是家庭成员涉嫌有贪污行为当然影响到中国领导层的形象，更重要的是现在中共领导层所说任何打击贪污的计划公信力是比较低的， 一般人都相信只要属于相当高层，党不会动你；就算动你也不是因为贪污而是一种政治斗争。<br />
