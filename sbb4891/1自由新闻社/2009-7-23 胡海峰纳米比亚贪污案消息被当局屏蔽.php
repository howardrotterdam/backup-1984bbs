<br />
 <br />
<br />
记得当年印尼总统苏哈托也不事必躬亲，多数是‘儿贪’‘孙贪’‘九族同贪’。早就听说Hu Jintao的儿子Hu Haifeng 是巨富，万万没有想到政治辅导员老子念念不忘‘洗脑’，儿子却精于‘洗钱’，洗的还是父亲(表面上)最同情的‘弱势群体’挣来的血汗钱。这回同方威视西窗事发在非洲Namibia，也象陈水扁家洗钱洗到了瑞士苏黎世银行。人云中国人贪，陈水扁说国民党贪，Hu Jintao说陈良宇贪，我不敢说国民党和陈良宇不是贼，但如今有事实可以证明Hu Jintao和陈水扁也是贼，不过台湾可以法办陈水扁，共产党又能不能法办这个伪善的大贼Hu Jintao呢？<br />
