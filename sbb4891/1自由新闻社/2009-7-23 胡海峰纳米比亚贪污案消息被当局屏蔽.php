																											<div id="postmessage_254309" class="t_msgfont">中国对非援助设圈套<br />
<br />
位于撒哈拉以南非洲的Namibia并不是一个世界级领导人经常光顾的地方，这个仅有2百万人口的国家（河蟹版此处有“基础设施极端落后”）道路崎岖，沙尘飞扬。因此，当中国国家主席Hu Jintao率领130多名随从官员在2007年2月份访问这个国家的时候，<strong>这显然不只是礼节性的拜访</strong>。<br />
<br />
不久以后，中国政府就批准了向Namibia提供低息贷款，利用这笔贷款Namibia向中国购买了价值5530万美元的中国制造的货物扫描仪以制止走私（参照河蟹版）。这是一个很干净的说明：中国表示，这笔交易不仅满足了Namibia的急需，而且也给中国带来了好处。<br />
<br />
看起来如此，直到Namibia控诉中方选择提供扫描仪的国有企业―最近由hu zhuxi的儿子运营的公司―促使了数百万美元的非法回扣交易；直到调查人员向Namibia寻求协助调查而中方出面阻碍。<br />
<br />
现在这些扫描仪看来也能解释一些别的：戴着光环的热心，秘密的“后门”交易，这些使中国对发展中国家的数亿美元援助款笼罩阴影。<br />
<br />
从巴基斯坦到安哥拉到吉尔吉斯斯坦，中国用他巨大的外汇储备巩固外交联盟，确保获得自然资源并且为其旗舰公司招揽生意。对外援助――通常降低利率贷款，有时用信贷捆绑更多的商业――就是这一努力的核心。<br />
<br />
发展中国家的首脑们拥抱中国推销的宽松信贷，而不是西方政治经济改革方式的要求，以成为未饱和需求的主人。这个结果从横贯非洲的公路，发电站，电信网络中可以清楚的看到，自2001年以来，超过200个项目，很多优惠贷款资助来自中国政府的进出口银行。<br />
<br />
尽管越来越多的专家争论中国援助的到来有一个主要企图：必须用于购买一些公司的产品或服务，这些公司有很多是国有的、中国政府指定选择的。中国不鼓励借款国竞标，并拉起了工程成本、贷款条例、还款条例等关键数据的面纱。<br />
<br />
反腐败人士抱怨说不透明导致腐败，腐败将减少外国的援助。<br />
