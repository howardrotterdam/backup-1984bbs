													<h2>纽约时报：胡公子在非洲 和谐版</h2>
						
						
																											<div id="postmessage_253957" class="t_msgfont">纽约时报：中国在非洲广施援助 经济已成外交后盾 <br />
<img src="http://i36.tinypic.com/35n8ro7.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
本文来自9月22日《纽约时报》，原文作者是SHARON LaFRANIERE，中国经济网现编译如下，仅供参考。<br />
<br />
&nbsp; &nbsp; 位于撒哈拉以南非洲的Namibia并不是一个世界级领导人经常光顾的地方，这个仅有2百万人口的国家基础设施极端落后，道路崎岖，沙尘飞扬。因此，当中国国家主席Hu Jintao率领130多名随从官员在2007年2月份访问这个国家的时候，盛大的场面简直就像是一场庆典。<br />
<br />
&nbsp; &nbsp; 不久以后，中国政府就批准了向Namibia提供低息贷款，利用这笔贷款Namibia向中国购买了价值5530万美元的商品。中国表示，这笔交易不仅满足了Namibia的急需，而且也给中国带来了好处。<br />
<br />
&nbsp; &nbsp; 中国向Namibia提供贷款只是一个典型。事实上，包括巴基斯坦、安哥拉、哈萨克斯坦和塔吉克斯坦等国都曾接受中国巨额援助贷款。这些贷款不仅促进了中国商品出口而且也成为中国推行外交政策的工具。中国提供的这些贷款绝大部分都是低息贷款，利息率远低于商业贷款利率。不过，这些贷款并非完全没有附加条件：大多数贷款的用途都必须得到中国政府的认可。<br />
<br />
&nbsp; &nbsp; 中国贷款给非洲大陆带来了巨大变化，一些国家修建了新的公路，发电厂和基础通信网络等。自2001年以来，中国在非洲资助了近200个建设项目，这些贷款大部分都是经由中国进出口银行承办的。<br />
<br />
&nbsp; &nbsp; 国际观察家们说，中国已经成为非洲大陆的一个主要角色，发挥着越来越大的作用。虽然中国向非洲国家提供的援助贷款不像西方国家那样附加许多政治条件，但是这些贷款通常都会给中国公司――尤其是国有公司带来巨大商业机会(本文来自《纽约时报》，编译时有删节)。<br />
