央视网消息(新闻联播)：中共中央总书记、国家主席、中央军委主席胡锦涛高度重视防治艾滋病工作，近年来曾3次在世界艾滋病日到北京市医疗卫生机构和居民社区考察。在第22个世界艾滋病日到来之际，胡锦涛总书记和中共中央政治局常委、国务院副总理李克强今天上午来到国家会议中心，参加首都防治艾滋病志愿者活动。胡锦涛代表党中央，向为防治艾滋病作出贡献的广大医疗卫生工作者和志愿者表示诚挚的问候。<br />
<br />
9点50分，胡锦涛等领导同志来到活动现场。场馆里，到处是鲜艳的红丝带，到处是热情的志愿者。<br />
<br />
胡锦涛首先来到活动的主题展示区。在一块块展板、一件件实物前，总书记认真听取介绍，详细了解全国和北京市防治艾滋病工作进展情况特别是志愿者开展活动情况。得知北京市防治艾滋病志愿者已发展到5万多人，他们深入社会重点场所和重点人群，做了大量富有成效的工作，总书记给予了充分肯定。胡锦涛强调，防治艾滋病关系到人民身体健康，关系到民族生存发展。当前防治艾滋病形势仍然严峻，必须毫不放松地抓好防治艾滋病工作。<br />
<br />
主题活动区里，志愿者们正在开展形式多样的防治艾滋病宣传活动。在同伴教育模拟课堂，胡锦涛饶有兴致地观看大学生志愿者通过小游戏普及艾滋病预防知识。在咨询热线展位，胡锦涛俯身浏览“首都红丝带”网站内容，向工作人员询问网站建设情况。他说，在互联网迅速发展的今天，网络为我们开展防治艾滋病工作提供了一个新的有效手段。希望“首都红丝带”网站越办越好，在防治艾滋病工作中发挥更大作用。<br />
<br />
联合国艾滋病规划署驻华办事处官员今天也来参加首都防治艾滋病志愿者活动。胡锦涛对他们表示热情欢迎，对国际组织和国际友人为中国防治艾滋病工作提供的支持和帮助表示表示衷心感谢。他说，艾滋病是全人类的共同敌人，防治艾滋病是全人类的共同责任。我们愿同国际社会加强交流合作，共同应对艾滋病这一全球性挑战，为有效推进防治艾滋病事业作出积极努力。<br />
<br />
在主题活动区现场，总书记通过视频通话系统，同地坛医院的医务人员和正在这里诊治的感染者亲切交谈。<br />
<br />
地坛医院负责人告诉总书记，他们医院按照总书记去年考察时作出的重要指示，加大防治艾滋病工作力度，开展了21项艾滋病科研课题，发展了2万多名志愿者，主动来接受艾滋病检测的人也越来越多。胡锦涛听后表示满意。<br />
<br />
胡锦涛一年前在地坛医院见过艾滋病感染者小郑，她经过服用免费母婴阻断药物，生下了一个健康的女儿。得知小郑的情况后，总书记从自己工资中捐出5000元，鼓励小郑增强生活勇气。<br />
<br />
随后，胡锦涛又来到主题互动区。在防治艾滋病志愿者招募站，许多年轻人正在领取志愿者报名登记表。总书记走过去，向工作人员了解志愿者招募情况。北京中医药大学博士生刘丹彤去年参加了北京奥运会志愿服务，今天又申请成为防治艾滋病志愿者的一员。她对总书记说，做一名志愿者很光荣，我是学医的，愿意为防治艾滋病尽一份力。总书记笑着说：“你有无私奉献精神，有从事志愿服务经验，还有医学专业知识，相信你一定能成为一名优秀的防治艾滋病志愿者。”<br />
<br />
在志愿者的热情邀请下，胡锦涛同大家一道动手制作起红丝带。红丝带做好后，总书记拿起一个，亲手贴到“爱心墙”上的心型图案中。顿时，由无数个红丝带组成的心型图案亮了起来。<br />
<br />
表演台上，志愿者们一边打着手语，一边演唱歌曲《微笑北京》。一曲唱毕，现场的人们纷纷围拢到总书记身边。胡锦涛对大家说，<br />
