<br />
　　按照工作纪律，“红袖章”当班时不允许随便坐下，要在一个区域不停巡视，“对不起同学,打扰一下，请你……好吗?”这是他们经常重复的话。如果哪位“红袖章”对不文明行为睁一只眼闭一只眼，被监察员看到，还会受到相应的处罚。<br />
<br />
　　“红袖章”们表示，他们的工作内容里，最多、最敏感也最难管理的就是情侣间的过分亲密问题。尤其是亲密到何种程度应该管，很难把握。一般来说，肯定要管的行为包括：一人坐或躺在另一人腿上、长时间拥抱亲吻等。<br />
<br />
　　“校园巡视岗既解决了近百人的助学岗位，又承担着维护校园文明环境的职责，是团委工作的一大创新。”南京林业大学一位负责人这样表示。<br />
<br />
　　据他介绍，“校园文明巡视岗”的设立已经有4年时间。现在，岗位上都是勤工助学的在校特困生。监督员主要负责劝止一些不文明现象，包括提醒那些有过分亲密行为的学生情侣。<br />
<br />
　　南林大校方对设立“巡视岗”的效果非常满意，现在情侣间的亲密行为收敛了很多，让校园看起来“很清爽”。“如果这些地方缺乏有效管理的话，在很大程度上会影响学校的整体形象。”这位负责人说。</div>

							
							
							
							
