Google said yesterday it was making its move because it detected a &quot;highly sophisticated and targeted attack on our corporate infrastructure originating from China&quot; in mid-December. Google said the attack resulted in &quot;the theft of intellectual property from Google.&quot; The company said that only two Gmail accounts appeared to have been accessed.<br />
