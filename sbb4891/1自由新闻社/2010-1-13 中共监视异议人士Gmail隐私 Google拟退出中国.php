Web Giant Says Cyber Attack Targeted at Least 20 Foreign Firms, Email of Rights Activists; A Major Challenge to Beijing<br />
<br />
Google Inc. said it may back out of China after an investigation found the company had been hit with major cyber attacks it believes originated from the country -- a move that would amount to one of the highest-profile rebukes yet of China by a major U.S. firm.<br />
<br />
Google said it believed the attackers were trying to access the Gmail accounts of Chinese human-rights activists. It said at least 20 other large companies were also targeted and that it was in the process of notifying those companies and working with U.S. authorities.<br />
