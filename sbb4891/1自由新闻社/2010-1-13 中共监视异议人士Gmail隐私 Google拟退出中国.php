<br />
The company said it will be talking with the Chinese government in coming weeks about how it might operate in China without censorship, long a thorn in the side of Western Web concerns operating there. &quot;We have decided we are no longer willing to continue censoring our results&quot; on its China Web site, Google.cn,&quot; the company's chief legal officer, David Drummond, said in the post.<br />
