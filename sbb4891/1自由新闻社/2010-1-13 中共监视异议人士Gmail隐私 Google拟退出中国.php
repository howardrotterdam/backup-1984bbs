																											<div id="postmessage_428250" class="t_msgfont">Google, Citing Cyber Attack, Threatens to Exit China<br />
By MIGUEL HELFT and JOHN MARKOFF<br />
<br />
Google threatened late Tuesday to pull out of its operations in China after it said it had uncovered a massive cyber attack on its computers that originated there.<br />
<br />
<strong>As a result, the company said, it would no longer agree to censor its search engine in China and may exit the country altogether.</strong><br />
谷歌公司可能不再过滤其中文搜索结果，并同时退出中国市场。<br />
<br />
<strong>Google said that a primary goal of the attackers was accessing the Gmail accounts of Chinese human right activists, but that the attack also targeted 20 other large companies in the finance, technology, media and chemical sectors.</strong><br />
谷歌公司表示，攻击者的主要目的是进入中国人权活动人士们的Gmail账户……<br />
<br />
In a blog posting by David Drummond, the corporate development and chief legal officer, Google said that it had found a “highly sophisticated and targeted attack on our corporate infrastructure originating from China.”<br />
<br />
<strong>“These attacks and the surveillance they have uncovered ― combined with the attempts over the past year to further limit free speech on the web ― have led us to conclude that we should review the feasibility of our business operations in China,” Mr. Drummond wrote in a blog post.</strong><br />
<br />
He wrote that Google was no longer willing to censor results on its Chinese-language search engine and would discuss with Chinese authorities whether it could operate an uncensored search engine in that country.<br />
<br />
<strong>“We recognize that this may well mean having to shut down Google.cn, and potentially our offices in China,” Mr. Drummond wrote, adding that the decision was being driven by executives in the United States, “without the knowledge or involvement of our employees in China.”</strong><br />
<br />
<strong>Google did not publicly link the Chinese government to the cyber attack, but people with knowledge of Google’s investigation said they had enough evidence to justify its actions.</strong><br />
谷歌没有公开表示中国政府与此次黑客袭击有关，但是了解谷歌调查结果的人士称他们有足够的证据证明这一点。<br />
<br />
A United States expert on cyber warfare said that 34 companies were targeted, most of them high-technology companies in Silicon Valley. The attacks came from Taiwanese Internet addresses, according to James Mulvenon, an expert on Chinese cyberwarfare capabilities.<br />
<br />
Mr. Mulvenon said that the stolen documents were sent electronically to a server controlled by Rackspace, based in San Antonio.<br />
<br />
“For Google to pull up stakes and basically pull out China, the attack must have been large in scope and very penetrating,” Mr. Mulvenon said. “This attack highlights the fact that cyberwarfare has basically gone to the next level.”<br />
<br />
<strong>Mr. Drummond said that Google decided to speak publicly about the attack not only because of its security and human rights implications, but because “this information goes to the heart of a much bigger global debate about freedom of speech.”</strong><br />
<br />
Google entered the China market in 2006, agreeing to introduce a censored search engine. At the time, the company said that it believed that the benefits of its presence in China outweighed the downside of being forced to censor some search results there, as it would provide more information and openness to Chinese citizens.<br />
<br />
But the company said that it would continue to monitor restrictions in that country and review its decision periodically.</div>

							
							
							
							
