<br />
Google's main Chinese competitors, especially Baidu, are seen as far more ready to comply with government-censorship rules, and have avoided the periodic blocking and official criticism that Google has experienced.<br />
<br />
Google's revenue in China is relatively small, with analysts estimating that only a few percentage points of Google's company's nearly $22 billion in 2008 revenue came from the nation. But the country's massive number of Internet users has made it strategically important for Google, as it tried to extend its dominance in search and search advertising around the globe.<br />
<br />
Google suffered another setback in September when Kai-Fu Lee, the high-profile former Microsoft Corp. executive it had hired in 2005 to lead its China operation, left to work on his own Chinese Internet venture.<br />
<br />
Google may go the way of other Internet companies, such as eBay Inc. and Yahoo Inc., which abandoned expansion plans in China in recent years -- although none of them in the publicly critical way that Google is suggesting. Both transferred their China businesses to local players in exchange for equity stakes.<br />
<br />
Foreign Internet companies have all struggled in China both against tough commercial competition and also government regulation and censorship.<br />
<br />
The common assumption, however, is that no matter how onerous the limitations and challenges faced by foreign companies in China, the market is too big and important to walk away from.<br />
<br />
That calculation has forced a number of foreign firms to accept conditions in China that they might not tolerate elsewhere. The country has 338 million Internet users as of June, more than any other country.<br />
<br />
Google would be the most high-profile Western company in recent years to draw a line under the kind of compromises it is prepared to make and walk away from China.<br />
<br />
It would be an extremely rare case of a foreign company taking a stand on human rights, and placing that issue over commercial considerations. A number of foreign companies exited China after the Chinese army crushed student protesters around Tiananmen Square in 1989. But they mostly came back in the following years.<br />
<br />
A Google withdrawal would also be an implicit rejection of the argument made by many technology companies that their presence in China overall helps expand access to information for Chinese citizens, despite censorship.<br />
