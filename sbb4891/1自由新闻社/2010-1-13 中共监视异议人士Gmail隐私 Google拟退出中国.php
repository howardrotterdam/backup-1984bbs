For Google to withdraw from China would be an extremely rare repudiation by a Western company of what is almost universally seen in big business as one of the world's most important markets. Even the public suggestion that it is considering such a move is likely to infuriate Chinese authorities.<br />
<br />
Chinese officials couldn't immediately be reached for comment. The government in the past has repeatedly defended its handling of the Internet, and has rejected accusations that China is responsible for cyber attacks against foreign entities.<br />
<br />
Google launched its Chinese-language search engine in 2006, agreeing to censor some of its results, a move that sparked sharp criticism from human-rights groups and Web-industry officials who are critical of any restrictions on the Internet.<br />
<br />
Tensions between Google and the Chinese government began soon, escalating in 2009 when Chinese officials reprimanded Google and accused it of having pornography on its sites; several Google services were temporarily inaccessible in China. Google's video-sharing site, YouTube, has also been inaccessible within China for the past number of months, and has been periodically banned in the past.<br />
<br />
Google's move comes as the company has been in negotiations with Chinese officials over various Google services in China. Last year, it agreed to remove some foreign-language links on its China homepage to placate Chinese officials.<br />
