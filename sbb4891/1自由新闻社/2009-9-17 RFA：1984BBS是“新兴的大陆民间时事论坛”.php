																											<div id="postmessage_245322" class="t_msgfont">中国要求各大互联网论坛新用户实名登记<br />
2009-09-07<br />
国庆之前北京又下达密令，新用户要在各大互联网的论坛发言，如新浪、网易、搜狐等，注册时必须先登记身份证号码。网民发现这两日论坛的发言明显减少。网络实名对言论构成立杆见影的“心理威胁”。不过，也有网民测试过，用身份证生成器产生的“假号码”登记，网站未有察觉，而旧有用户未受影响。（何山报道）<br />
<br />
<br />
大陆的网友在最近两日发现，要在国内的入门网站登记发言，新用户不登记身份证号码不行。即是说，网络实名制已经悄悄在国庆之前实施。大陆活跃的独立网民北风对本台讲，“都是这两天，不过在网上面很容易找到身份证号的生成器，随便都可以作一个出来，转成实名注册，我想很多人不会注册。”<br />
<br />
其实，早在今年的7月底，香港亲北京的大公报已经率先披露，负责监督中国网路的主要政府机构，中国国务院新闻办公室下达了通知，要实施网络实名制。而业内人士，一开始就预期，网路实名制必然会引发更多的网民使用假名、假电邮、假身分证号码、假手机号码等，使当局与网友的”猫捉老鼠”游戏必然要再升级。<br />
<br />
因为在网路，上载了福建严晓玲疑遭轮奸致死录像，之后被警方“请饮茶”长达16日 的网民郭宝峰就对本台讲，新的制度是一种恐吓。他说：“已经确认过了，他是用了假的身份证，暂时还没有跟公安局的讯息联网，但是这个系统已经是真的，确实是真的。”郭宝峰说，新措施与当局害怕邓玉娇案、严晓玲案、艾未未四川为谭作人作证等网络热点，引发网民舆论监督有关。他说：“因为主要是很多事件，今年我的事件，艾未未的事件，当局是怕了网络的民意在影响政府决策。”<br />
<br />
不过，由于国内的入门网站，当下还没有与官方的“公安网”联网，用假身分证号，还是可以成功在众多主要网站成功注册。而当局监控言论的手段升级，已经压抑了网民发言的意欲。北风说：“他没有要你传回去身份证的复印件或者是扫瞄相片，有些网友就发觉，网站评论的人是少了很多，这是真的。”<br />
<br />
有学者就认为，随著落实网络实名制，其引伸的“假名”、“假身份”泛滥，使大陆要建立电子商贸更加困难。而网民对政府及网站的不信任，对中国互联网的长期发展更为不利。网民郭宝峰说，“有点是威吓，起到威吓的效果。你身份登记的话，他是担心我发的一个贴子为被请去喝茶，以前就不一样，以前可能这种顾虑会少一些。”<br />
<br />
<strong>目前，新的规定还未影响到原先已经在网站注册、登记的网友。旧网民不用身份证，用旧有登录仍然可以登入使用。为免被网监出卖IP身份、送官查办，有网民则是索性建立有别于官方的论坛及告示版。新兴的大陆民间时事论坛，有如以奥威尔名著《1984》为题的1984BBS兴趣小组，以及早年就建立的自由中国论坛等。</strong><br />
