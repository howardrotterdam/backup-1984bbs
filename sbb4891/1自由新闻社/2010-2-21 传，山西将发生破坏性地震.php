<br />
“在1月6日至13日，根据省政府安排，省地震局对全省地震应急预案实施情况进行专项检查，很多部门根据预案进行了地震应急演练，这本是正常活动，却被部分市民误认为要发生地震了。”昨日，山西省地震局办公室相关负责人表示，地震谣言源于市民对地震应急演练的误解。<br />
<br />
短信传播“太原要地震”<br />
<br />
1月15日，家住太原的王先生收到朋友转发的一条手机短信，短信称:“最近各大医院正在搞防震演练，并且储备医疗用品，还选派很多医生和护士作为地震应急人员，看来太原近期会发生大地震，请做好防震准备，尽量不要在建筑物内逗留。”<br />
<br />
此后，“地震传言”进一步升级，有了另一个版本:“我已经看过山西省地震局网站上的地震目录，上面写着六级以上地震。”在该传言里，还注了一个网址。<br />
<br />
记者打开网址发现，正是省地震局官方网站的“山西地震目录”，而上面并没有发现传言中的“六级以上地震”。山西地震局办公室张主任解释说，该地震目录每周公布一次，最近并没有监测到有感地震，部分市民所说的“六级以上地震”，是对地震目录的误解。“在目录中，除了后面的地点外，基本上都是数字和字母，部分网友把前面的编号误解为震级了，其实只有‘ML’后面标记的才是震级。”<br />
<br />
原因是应急演练遭误解<br />
<br />
张主任告诉记者，为认真落实新的《防震减灾法》，汲取汶川地震经验教训，我省努力提高防震减灾综合能力，第五次修订了《山西省地震应急预案》，并且根据省政府安排，在今年1月6日至13日，对预案涉及的军区、卫生、民政、宣传等部门进行为期一周的检查。<br />
<br />
“检查的目的，主要是看各个部门对预案的落实情况。为此，很多部门还专门开展各种地震应急演练活动。”张主任说，医院等单位的正常演练活动，遭到部分市民误解，以为要发生地震了。<br />
<br />
张主任表示，根据《防震减灾法》相关规定，地震预报只有省政府才有权发布，任何单位和个人均不得发布地震预测信息。“传播地震谣言是违法的，希望收到相关短信的市民不要再进行传播。”<br />
