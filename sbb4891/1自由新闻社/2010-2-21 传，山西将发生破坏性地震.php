话你知：元朝 8级强震袭山西 <br />
<br />
山西虽然不是地震活跃地区，但自有史料记载以来，当地曾经发生黎克特制 5级以上地震超过 1,200次。专家指出，在元朝大德七年八月初六（即 1303年 9月 17日），洪洞赵城一带发生 8级大地震，《元史》和诸多历史文献以「不可胜计」、「靡有孑遗」来描述是次地震的破坏，「或概略为数万、数十万，灾害十分严重」。近代最强的一次地震发生于 1989年 10月 18日，大同、阳高一带发生 6.1级大地震，造成 19死 193伤，灾区八成房屋倒塌。人民网 <br />
