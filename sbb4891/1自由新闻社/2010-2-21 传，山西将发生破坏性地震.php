 <br />
市民凌晨带着行李慌忙走上太原街头。互联网<br />
<br />
有人全屋家电搬到街上&nbsp;&nbsp;<br />
 <br />
地震传言令山西数百万居民昨日凌晨走出居所，有人手抱儿子冒严寒到街上避难。互联网<br />
<br />
类似情况在太原、晋中、长治、晋城等地发生，街头、公园人满为患，通宵营业快餐店、油站等更是逼满民众，人人拿着电话通知亲友「走难」，多个城市地震局的网站被挤爆了，地震局的电话也打不进去。在祁县、平遥、左权等乡村，村官通过广播叫醒熟睡村民起床避灾，有村民更为保护财产，将全屋家电全部搬到了街道上，来减少财产损失。一名家住山村的网民也称，他凌晨 4时 10分被亲戚打电话吵醒，在「地震吧」、「山西吧」等网上论坛发现异常热闹，几乎全部是山西人在讨论有关地震的帖子，大家都互相通报着山西各地的情况。「有不安，有戏谑也有咒骂」，由于预告指大地震时间在凌晨 4时到 6时，估计昨日山西省有数百万人出外彻夜等地震。有网民叹说：「全山西都 TM（他妈）不睡觉了!」直至上午 6时许，太原市地震局及各地地震部门，才陆续转发了山西省地震局的「紧急公告」，声称：「『近日有地震』的传言请大家不要信传，保持正常生活、生产秩序。」当局又强调只有省政府才能发布地震预报，其它任何单位、个人都无权发布。「这通知起多大作用？」政府的辟谣通知一出，实时引发民众不满，许多人表示不再信任地震局。因为今年 1月中当地民众纷纷接获短讯，称近日省内各大医院进行防震演练、大量储备医疗用品，看来太原会发生地震，更有网民称可能会发生六级以上地震。当局实时将消息定性为「谣言」，呼吁民众不要轻信、传播，但话音未落， 1月 24日上午运城河津市、万荣县交界则发生黎克特制 4.8级地震，并发生十多次余震，幸未造成大破坏。地震局事后回应指：「地震不够大毋须预报」。新华社/人民网 <br />
