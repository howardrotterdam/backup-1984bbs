「快地震了」全山西不敢睡数百万人凌晨涌上街　当局清晨辟谣 <br />
<br />
「三更夜半，街上都是人！」山西省各大小城市昨日凌晨陆续接到 4时至 6时将会发生大地震的预告，并且迅速传播，包括省会太原等多个城市数百万人夜半惊醒，涌到街上避难「等地震」，街上人头涌涌、车水马龙、快餐店与油站等都是人。不少民众更将电器、财物搬到街上。当局让民众苦候数小时，至清晨过了地震预警时间才发出通知辟谣，引起非议。<br />
<br />
「将会发生破坏性地震」的消息自昨日凌晨零时起，在山西各大城市陆续传出，引起广泛恐慌。一名家住吕梁地区的网民称，凌晨接到一个又一个亲戚电话，不停地说：「快地震了，赶快起来！」当他起床、走到街上，发现大街上全是人，广场上更是人头涌涌，很多人穿上御寒衣物、开着汽车、带着行李、宠物狗，在空旷地方等待大地震发生，不少避难的更是拄着拐杖、年过八旬的老人。<br />
 <br />
