惊魂源于地震部门信誉破产 <br />
<br />
2008年 5月 12日四川汶川发生 8级特大地震，有关当局事前毫无预警，令天府之国民众生命财产遭受毁灭性打击。内地学者指，汶川地震宣告中国地震部门的信誉已破产，山西地震流言，则源于政府诚信破产，这是山西昨日地震恐慌的主因。<br />
<br />
「当官的越说没有越要小心」&nbsp;&nbsp;<br />
 <br />
四川大地震后，内地有媒体曾披露，大地震前曾有地震专家预测到将有地震发生，但上报国家地震局后，不被理睬，有关专家反而遭到排斥，事件虽然在当局压制下没有炒大，但民众对内地地震部门的权威已产生怀疑。上月底山西也曾传有地震，但当局高调否认，结果 1月 24日山西运城果真发生 4.8级地震，虽未造成重大伤亡，但当地民众已大呼：「当官的越说没有，越要小心！」北京媒体人士刘红庆指，汶川大地震宣告了中国地震部门信誉破产。昨日山西这场地震惊魂，当局声称源于流言，却不知流言源于民间对官方不信任；地震部门搞演练事先不跟民众充份沟通，民众没有安全感，又不知向谁了解真相，只好盲从；政府在公众心中的诚信已经破产。有网民指，一个小小流言竟然蛊惑了山西半个省的民众，数百万人要在萧瑟的寒夜中出街挨冻，山西当局真应该反思一下其中的原因，「假地震尚且了造成如此大的混乱，万一真地震发生，谁来提醒和保护我们？」本报记者 <br />
