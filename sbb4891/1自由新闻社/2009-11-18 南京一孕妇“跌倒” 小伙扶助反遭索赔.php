<br />
&nbsp; &nbsp; 昨日上午10时许，市民郑先生赶到南京火车站，准备前往上海。他下了33路公交车往火车站候车大厅方向赶去时，“扑通”一声，前面一女子被一个奔跑的小男孩撞跌倒了。一开始郑先生并没有注意，他向前走了四五步猛然发现，跌倒在地上的女子是一名孕妇。此时孕妇斜躺在地，手捂着肚子，面部表情十分痛苦。“小妹子，怎么样？不会有问题吧？要不要叫120……”郑先生关切地连连询问。<br />
<br />
<br />
&nbsp; &nbsp; 此时孕妇看也没看一眼就质问郑先生：“不长眼啊？”随后孕妇试图一只手支撑在地上爬起，可又很艰难。郑先生见此，忙将孕妇从地上扶起。当时广场上还有其他旅客，见状都围上去。就在郑先生转身要走时，孕妇突然上前攥住了郑先生的衣角大喊：“就是你把我撞倒的，你不要走！”双方正在争执时，一名女子也凑上来说：“我看到了，就是你撞的！”该女子还强调自己与摔跤的孕妇“素不相识”。不一会儿，孕妇打电话将她丈夫叫来。<br />
<br />
<br />
&nbsp; &nbsp; 其丈夫赶来，一把拽住郑先生就挥拳要打，并问郑先生，“既然人不是你撞的，你为什么要发善心去扶，广场上来来往往人多着呢，就你一个人学雷锋？”孕妇丈夫问得郑先生无言以对。先前“素不相识”女子又说是郑先生撞的人。孕妇夫妇见有了“证人”，更认定是郑先生的责任，继续纠缠，并提出拿2000元钱作为赔偿。无奈之下，郑先生只好报了警。<br />
<br />
<br />
&nbsp; &nbsp; “我和这位大姐很熟，她不会故意为难别人的。”当车站民警赶到现场时，原本自称不认识孕妇的女子突然改口。郑先生马上回击说，你刚才说和她不认识，现在又说认识了。就在这时，前来托运行李的一位姓王的男子向民警描述：“当时，我正好路过，孕妇倒下时，就在我旁边不到两米处，我想去扶，旁边更近的这位大哥已经扶了，然后我就走了。她其实是被之前路过的一个男孩蹭了一下，才失去重心跌倒的。”见王某来作证人，孕妇夫妇也不答应，说王某是与郑先生一起的。<br />
<br />
<br />
&nbsp; &nbsp; 双方说法一时难住了民警。民警从广场监控录像里调阅了事件经过，正如郑先生所说。随后，孕妇及其丈夫向郑先生道歉，但被郑先生拒绝了。（卢斌） <br />
