2010-06-30 19:18:00　来源: 新华网(广州)　跟贴 1046 条 手机看新闻<br />
<br />
核心提示：28日，谷歌高管发文称谷歌中国网站提供了新的访问页面确保符合中国法律的同时不对搜索结果进行审查。谷歌在华合资企业已向有关部门递交ICP牌照年检整改材料和“年检承诺书”,明确承诺“我们将继续遵守中国法律”,新华网发文批评谷歌企图两边讨好的做法。<br />
<br />
新华网北京6月30日电 美国谷歌公司的大卫 德拉蒙德6月28日又发博客文章了,说谷歌中国网站提供了新的访问页面,声称“这个新做法确保了Google.cn不对搜索结果进行审查的承诺,并且,我们相信,符合中国法律”。德拉蒙德还想如1月12日“扬言退出中国”和3月23日“将部分搜索业务转至香港”那两次发表博文一样引起关注和“轰动”,但这回他恐怕要失算了。<br />
<br />
人们还会记得,3月23日德拉蒙德博客文章发表后,国务院新闻办网络局负责人立即发表谈话,表明中国的严正立场。谈话中指出:“如谷歌公司愿遵守中国法律,我们依然欢迎谷歌公司在中国经营和发展;如谷歌公司执意将谷歌中国网站的搜索服务撤走,那是谷歌公司自己的事”。这里说的再清楚不过,即外国企业在中国经营就必须遵守中国法律,这同中国企业到国外经营也要遵守所在国法律一样,是最起码的要求,天经地义、无可置疑。<br />
<br />
记者从政府有关部门了解到,经营谷歌中国网站的谷歌公司在华合资企业是谷翔公司,这家公司要继续取得ICP许可证,关键是要承诺在华经营遵守中国法律。几乎就在6月28日德拉蒙德先生发表博客文章的同时,谷翔公司已向政府有关部门递交了ICP牌照年检整改材料和“年检承诺书”,明确承诺“我们将继续遵守中国法律”,承诺本公司明确知晓所提供和接入的信息内容,保证不存在《电信条例》第五十七条规定的违法信息,并接受主管部门的监督检查。<br />
<br />
记者查阅了《电信条例》第五十七条规定,内容是:“任何组织或者个人不得利用电信网络制作、复制、发布、传播含有下列内容的信息:(一)反对宪法所确定的基本原则的;(二)危害国家安全,泄露国家秘密,颠覆国家政权,破坏国家统一的;(三)损害国家荣誉和利益的;(四)煽动民族仇恨、民族歧视,破坏民族团结的;(五)破坏国家宗教政策,宣扬邪教和封建迷信的;(六)散布谣言,扰乱社会秩序,破坏社会稳定的;(七)散布淫秽、色情、赌博、暴力、凶杀、恐怖或者教唆犯罪的;(八)侮辱或者诽谤他人,侵害他人合法权益的;(九)含有法律、行政法规禁止的其他内容的”。谷翔公司在年检材料中表示,“如果存在问题,将依法接受主管部门处罚”。这表明,谷翔公司接受了有关部门提出的年检整改要求,应该说这是个进步,值得欢迎。<br />
<br />
据了解,谷翔公司递交年检整改材料的时间很晚,有关部门正在抓紧研究,相信很快会有结果。<br />
<br />
现在回过头看一下,德拉蒙德6月28日的博客文章就露出了“破绽” 明明是经营谷歌中国网站的谷翔公司明确承诺“我们将继续遵守中国法律”、接受主管部门依法监管,而在德拉蒙德的最新博客文章中却变成“确保了 Google.cn不对搜索结果进行审查的承诺”。德先生如何自圆其说?企图两边讨好,实际上是既想在西方政治上得分,又想在中国经济上得利。<br />
<br />
德拉蒙德拿谷歌中国ICP牌照年检的问题说事,实在是自讨没趣。<br />
