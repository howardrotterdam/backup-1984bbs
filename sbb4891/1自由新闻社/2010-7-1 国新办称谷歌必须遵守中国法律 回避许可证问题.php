													<h2>环球时报:谷歌申请新牌照系向中国法律低头</h2>
						
						
																											<div id="postmessage_717566" class="t_msgfont">环球时报:谷歌申请新牌照系向中国法律低头<br />
2010-07-02 10:39:00　来源: 环球时报-环球网(北京)　<br />
核心提示：谷歌想留在中国，保全所谓“Google.cn”的经济利益，又想用提示网址的方法，在西方舆论继续得分,但是这种把戏看似取巧，实则拙劣。<br />
<br />
环球时报-环球网7月2日报道 谷歌公司想留在中国吗？当然想。否则，它可以彻底废掉“google.cn”的网站，它在华的合资公司谷翔公司也大可不必再向中国政府申请ICP经营牌照。说到底，在中国法律面前，谷歌最终还是低下了傲慢的头。<br />
<br />
不过，谷歌公司似乎并不善罢甘休。一面让代理商申请牌照，一面又让自己公司高层发博客文章，宣称“要确保Google.cn不对搜索结果进行审查的承诺”。于是，全球网络史上从未出现过的奇怪搜索现象出现了，点击“Google.cn”就会有“我们已移至Google.hk.com”提示，再点一下就到了“Google.hk.com”。<br />
<br />
换句话说，谷歌想留在中国，保全所谓“Google.cn”的经济利益，又想用提示网址的方法，在西方舆论继续得分,但是这种把戏看似取巧，实则拙劣。<br />
<br />
过去几个月，谷歌是够能折腾的。在世界各地，谷歌公司虽标榜“不作恶”的商业原则，却到处兴风作浪，受到了许多国家的谴责。在中国，谷歌公司的反反复复，牵涉了多少人的精力，干扰了多少人的生活，现在，中国网民已对谷歌很不耐烦了。<br />
<br />
早有调查显示，约87%的受访网友对谷歌退出“无所谓”，只有约13%的受访网友表示“很可惜”。越来越多的中国老百姓看清了谷歌的两面手法。中国人真是希望谷歌能老老实实在中国做生意，如果它真的愿意的话。<br />
<br />
事实上，笔者获悉，谷歌还是真想留下来的。数周前，谷歌副总裁曾向中国政府做出承诺：作为一个国际大公司，尽管（继续留在中国）这样做会遭到某些媒体和政治家批评，付出一定代价。将来到美国和欧洲议会作证，谷歌公司也会明确表示，在中国的业务必须遵守中国的法律。<br />
<br />
如果这番话不是谎言，而是谷歌有诚意的承诺，那么谷歌就应该100%履行承诺，不要老想着什么便宜都占。中国政府的态度向来明确：“如谷歌公司愿遵守中国法律，我们依然欢迎谷歌公司在中国经营和发展；如谷歌公司执意将谷歌中国网站的搜索服务撤走，那是谷歌公司自己的事。”<br />
<br />
既想拥有经济利益，又想在西方舆论的政治道德上得分，到头来很有可能是竹篮打水一场空。现在，谷歌的点击量和用户量正在快速下滑，谷歌公司的形象即便在西方世界也越来越多地受到诟病，其“不作恶”的原则几近成了国际舆论中的笑柄。<br />
<br />
在这种情况下，谷歌公司的高层应该好好反思一下。为什么一个曾领衔世界互联网潮流的高科技公司，会这样一点点地退步和蜕变呢？信息化时代的市场竞争，就像逆水行舟，稍有不慎就全军覆没，大公司很快倒闭不乏先例。再这样下去，谷歌公司还真应该想想未来了？<br />
<br />
现在，谷翔公司的年检整改材料才刚刚递交。中国有关部门正在研究，相信不久会有结果。不过，要想让结果如谷歌公司所愿，最终大家皆大欢喜，还要看谷歌的最终表现。看起来很是聪明，其实是自曝其丑，自讨没趣的手法是不可能得利的。▲（作者是北京某研究机构研究员。)<br />
<br />
