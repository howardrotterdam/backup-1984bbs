													<h2>国新办称谷歌必须遵守中国法律 回避许可证问题</h2>
						
						
																											<div id="postmessage_714727" class="t_msgfont">中国监管机构周三对于是否会允许谷歌(Google)保留中国网站保持缄默。此前，这家美国公司做出了最新妥协举动。<br />
<br />
国务院新闻办公室主任王晨对记者表示：“我们一贯主张，要在中国发展，企业就必须遵守中国法律。”<br />
<br />
王晨是中国负责审查互联网的官员之一，但负责更新谷歌互联网内容提供商许可证的监管机构是工业和信息化部。<br />
<br />
人们本来希望，谷歌将中国网站用户自动转往无审查的香港网站之举，已经解决了该公司与中国政府在网络审查问题上的纠纷。但周二谷歌让这种希望化为泡影。<br />
<br />
谷歌表示，如果该公司继续转接用户，北京方面不愿更新其互联网内容提供商许可证（该许可证是运行Google.cn网站的前提条件）。作为回应，谷歌在Google.cn上创建了一个“着陆页面”，向访问者提供香港网站的可选链接，以最后努力挽救其在中国的业务。<br />
<br />
中共机关报《人民日报》旗下的小报《环球时报》援引学者的话称，谷歌的举动只是一个技术变化。该报称，这家公司没有改变其态度。<br />
<br />
知名博主、互联网评论员北风在推特(Twitter)上表示：“我认为主管机构不会再给Google.cn任何机会。”<br />
<br />
许多同情谷歌的中国网民表示，不值得与政府对抗。<br />
<br />
北京方面给谷歌设定了最迟在周三递交其互联网内容提供商许可证年度更新申请的期限。<br />
<br />
但是，由于互联网内容提供商许可证的有效期为五年，而谷歌的许可证要到2012年才失效，因此政府在做出最终决定方面没有时间压力。观察人士表示，很难预测政府需要多久才对谷歌的最新举动做出回应。<br />
<br />
另一方面，政府发出了又一个消极信号：批准多家中国公司提供在线地图服务的牌照申请，但不包括谷歌。国家测绘局发布了18家公司的名单，称这些公司已获得批准，其中包括中国在线搜索市场的本土领先者百度(Baidu)。<br />
