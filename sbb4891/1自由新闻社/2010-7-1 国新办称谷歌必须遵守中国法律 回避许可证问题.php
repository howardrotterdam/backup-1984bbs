													<h2>人民网：谷歌cn域名恐难保 “跪求”网民收藏hk域名</h2>
						
						
																											<div id="postmessage_716080" class="t_msgfont">尽管谷歌能够取得ICP牌照和地图牌照没有直接关联，但谷歌先期处理与中国政府关系时表现出的莽撞态度恐怕不会有利于其树立一个“中国政府信赖企业”的形象<br />
<br />
　　<br />
<br />
　　“我们已移至google.com.hk，请收藏我们的新网址”。昨天，点击google.cn域名，用户会在搜索输入框下看见上述语句。除了搜索框，整个谷歌中国首页已经没有其他诸如“视频”、“地图”、“图片”等在内的搜索细目链接，页面略显简陋。<br />
<br />
　　按“链接”索骥，进入谷歌中国新网址，搜索框下方，标注着“把google加入收藏”的蓝色链接块在整个页面上地位突出。<br />
<br />
　　这或许意味着谷歌中国保留cn域名的努力不会得到满意结果。谷歌不得不从现在开始为后cn域名时代做准备。<br />
<br />
　　“谷歌中国看来在为拿不到互联网内容提供商（ICP）牌照，放弃cn域名做准备了。”互联网实验室总裁刘兴亮接受《国际金融报》记者采访时说。<br />
<br />
　　昨天，谷歌中国的ICP牌照到期。截至记者发稿时，该公司没有对是否取得ICP牌照给出官方说法。<br />
<br />
　　6月29日，谷歌中国曾宣布停止把登录谷歌cn域名的用户自动跳转至hk域名的“打擦边球”做法。当天，谷歌还在官方博客宣称“已经找到解决与中国政府之间矛盾的办法”。在中国政府明确表态不赞同“跳转行为”后，谷歌中国主动停止跳转，一度被解读为摆出姿态，为留住cn域名做最后努力。<br />
<br />
　　仅一天后，谷歌的cn域名保护行动似乎已显出颓态。失去cn域名后，用户必须直接依靠hk域名访问谷歌中国。显然，抢在cn域名失效前用醒目方式请用户收藏hk域名，恐怕是谷歌中国最大限度保持用户粘性的补救之举。<br />
<br />
　　另外，谷歌的牌照麻烦还不仅局限在ICP牌照上。国家测绘局6月29日在官方网站上发布的甲级测绘资质审查意见公示显示，已提交在线地图和地理服务牌照申请的谷歌不在公示的名单中。与此同时，竞争对手百度和搜狗等13家公司进入了公示名单。<br />
<br />
　　“虽然这两个牌照没有直接联系，但目前看，谷歌有可能两个牌都拿不下来。”刘兴亮不无感慨。<br />
<br />
　　拿不到ICP牌照，谷歌中国还有hk域名补救，拿不到地图业务牌照，谷歌增长势头最猛的地图业务恐怕会在中国遭遇大麻烦。<br />
<br />
　　谷歌地图服务提供商北京谷翔信息技术有限公司是谷歌与赶集网共同组建的合资公司。该公司要在中国正常运营，地图牌照是绕不过去的坎。<br />
<br />
　　据记者了解的情况，地图牌照还将延伸到手机地图服务领域。而手机地图服务则被看成3G时代手机的重要运用之一，被谷歌寄予厚望。<br />
<br />
　　国家测绘局副局长宋超智曾称，应用于互联网地图的高清晰卫星影像大多来源于国外，给我国国家安全造成巨大隐患。颁发地图牌照，有助于防止泄密。<br />
<br />
　　尽管谷歌能够取得ICP牌照和地图牌照没有直接关联，但谷歌先期处理与中国政府关系时表现出的莽撞态度恐怕不会有利于其树立一个“中国政府信赖企业”的形象。<br />
<br />
