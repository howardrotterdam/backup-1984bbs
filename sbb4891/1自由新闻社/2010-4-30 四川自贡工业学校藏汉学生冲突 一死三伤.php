&nbsp; &nbsp; <br />
&nbsp; &nbsp; 继自贡市轻工工程学校，在半个月前发生藏汉学生冲突后，本周日，自贡的另一所有藏人就读的学校也发生藏汉学生冲突，双方十多人动用刀具厮杀。据本台公民爆料箱接获的该市釜溪职业高级中学的一名藏族学生发来的电子邮件称，周日凌晨发生的这次冲突本身规模很小，参与者只有十余人，但双方都使用了刀等杀伤性极大的武器，死亡学生叫巴桑（音），而三名受伤学生的情况尚不是很清楚，但他们被警方隔离。事件发生后，四川省政府已介入此事，随后在该校开会，作出了对藏族学生公布此事，对媒体和汉族学生封锁消息的决定。而本周二自贡警方在各学校开展收缴刀具的活动。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 本台记者周四致电该校办公室和多个部门，电话始终无人接听。记者于是致电该校一位员工：“周日凌晨你们那里又发生冲突了？”<br />
