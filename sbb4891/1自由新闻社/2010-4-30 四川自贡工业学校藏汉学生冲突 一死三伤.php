													<h2>川校园民族冲突 死者家属逾百人抵自贡</h2>
						
						
																											<div id="postmessage_595098" class="t_msgfont">川校园民族冲突 死者家属逾百人抵自贡 <br />
(博讯北京时间2010年5月03日 转载)<br />
&nbsp; &nbsp;&nbsp;&nbsp;四川自贡职业技术学校（又称自贡工业学校）上周日爆发的民族冲突，被刺死的藏族学生巴桑，其家属一百多人上周赶到自贡，了解真相，要求严惩凶手。据知情者告诉本台，公安大为紧张，已派专人安置家属，令当局棘手的是行凶者马文才是彝族，被以“防卫过当”刑事拘留。自由亚洲电台特约记者乔龙的采访报道。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;本台上周独家报道，自贡职业技术学校（又称自贡市工业学校）上个星期天，发生民族冲突，1名藏族学生死亡，3人受伤。该校严密封锁消息，而自贡市公安局上周四对本台证实事件。据知情者告诉本台，事发后，公安已全面加强对校园的监控，严禁外人进入该校，校门口还有公安，同时向该校的每位藏族学生赠送手机，进行安抚：“在学校门口有三个警察站岗，然后，每个人出入都要凭出入证来证明身份，在学校外面可以看见四川省人民政府联合中国移动，赠送藏区学生每人一部手机的公告”。 (博讯 boxun.com)<br />
<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 本台星期天再次致电该校办公室，但电话无人接听，记者又向该校一名员工查询：“少数民族学生打架的事件，您听说了吗？”<br />
&nbsp; &nbsp; 员工：是有打架的事情，是有这个事。<br />
&nbsp; &nbsp; 记者：好像有一个藏族学生打死了。<br />
&nbsp; &nbsp; 员工：这个事情不是很清楚，具体问学校吧。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 当地一名学生告诉记者，当地的媒体和该校学生都接到禁止向外界透露消息的通知：“封口令是绝对不能允许报道这一事件，所有的老师都被要求统一口径，都要说‘不知道’或者说‘听说过一点，但不是很清楚’而领导会被要求说‘希望去找省外宣办或自贡市外事委员会这样的部门’”。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 知情者表示，发生冲突的有学生分别有汉族、彝族及藏族，而刺死藏族学生桑巴的是一名彝族学生马文才，令当局对该案的定性，非常谨慎：“目前初步定性为‘防卫过当’，因为首先是死者先捅的人，而捅死人的彝族学生马文才，他是夺下他的藏刀，然后，把对方的喉管割破以后，造成他的死亡。因为也不知道是否会涉及到某些民族政策上的问题，所以这件事情还要详细的报到公安部，然后和省人民政府，共同来协商，进行最后的定性”。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 要求匿名的知情者告诉本台，三名伤者被安排入住当地的解放军第五十四医院，与外界隔离，门外还有岗哨。上周六起，该医院一度暂停对普通百姓门诊。而死者巴桑家属，这次来了一百多人要求了解真相，公安却唯恐事态扩大： “死者的家属在当地确实是一个名门望族，他们到自贡来进行安葬死者的人数有一百多人，这些家属已经被警方严密的保护。基本上没有什么人知道目前在哪个地方。隔离伤者的医院是解放军第54医院，已经暂时不对外面的群众开放”。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 自贡在一个月内先后发生轻工工程学校和职业技术学校，两起民族冲突事件，原因究竟何在？本台在百度贴吧看到，自贡职业技术学校的汉族学生写道，国家负担少数民族学费，每天发10元生活费，他们免费读书，还发钱，汉族学生没有优惠。另有学生称，汉族学生被校方要求“对藏族学生要打不还手”，也是歧视。近期，汉族学生联合来自凉山的彝族，共同对付藏族学生。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 据了解，自贡市有多所民族学校，而发生冲突的两所学校近期上周通知藏族学生，除了向他们赠送手机，又传出四川省委将派高官到自贡和他们座谈及宴请他们。釜溪职业高中学校的一名藏族学生称：“省委书记很可能会请自贡的发生民族冲突的两所学校的学生吃饭，但是引起了其他学校学生的不满，我们学校的藏族学生，他们也想省委书记请他们吃饭，他们是不是又要找人打架呢”。<br />
