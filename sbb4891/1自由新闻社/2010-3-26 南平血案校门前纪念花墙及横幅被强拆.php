家长：“花墙怎么会影响孩子?女儿还用纸条在上面写着‘一路走好’” 羊城晚报特派记者 邓勃 摄<br />
<br />
羊城晚报3月26日报道 今晨7时15分，羊城晚报记者再次来到南平市实验小学门口准备采访。昨日下午，一位市关工委的负责人曾向记者透露，今天早上将举行一个特殊的“告别仪式”―――那些遇难学生的课桌将在这个仪式后被撤走。但记者却在采访现场遭遇自称是当地政府官员的人阻挠，此人当时正在拆除悼念“花墙”。<br />
<br />
记者赶到实验小学时发现，校门对面的铁栅栏上挂满了长约12米的鲜花和花环，几幅写着“安息吧!不幸的小花朵”、“亲爱的孩子，去天堂一路走好!”的白底黑字四方条幅格外醒目。但一位戴眼镜的中年女教师一口否认上午将会有“告别仪式”，同时还要两名同事将那两块横幅拆下来，此举遭到现场多家媒体的质疑，该教师解释：“花和横幅挂在这里，孩子们会害怕!”<br />
<br />
“标语和花墙是市民的自发悼念行为，你们是否有权利这样做?”在记者的追问下，该教师请出了一位自称在“市委”工作的官员，横幅被迅速拆了下来，该“官员”还严肃告诫记者们：“不许拍照，不许摄像!”<br />
<br />
<br />
话音刚落，一位母亲带着女儿走了过来。母亲红着眼睛告诉记者，7岁的女儿昨晚在家里一笔一划地写下了“一路走好”的字条，早早起床后，准备让妈妈带她来这里挂在“花墙”下。“花墙和横幅的存在是否会加剧学生的恐惧心理?”记者决定对周围群众做现场调查，大多数回答是“二者应该没有必然联系”。正采访中，记者突然被一名头发稀少的男子揪住了衣领，记者认出他是南平市委宣传部通联科的负责人。<br />
<br />
此人命令记者交出记者证，现场群众立即嘘声四起，有人说：“就算不是记者也有权利站在这里吧!”该官员立即回应：“全南平的记者都归我管，都必须给我看记者证!”并威胁羊城晚报记者：“你在这里煽动和挑拨群众。你等着，我有办法收拾你!”说罢，走进了学校。<br />
