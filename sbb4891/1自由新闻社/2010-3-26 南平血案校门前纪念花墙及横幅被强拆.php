													<h2>南平校园血案环卫工保护学生无缘见义勇为</h2>
						
						
																											<div id="postmessage_536693" class="t_msgfont">左脸上至今还留着伤疤的环卫工人刘瑞英，如今依然会在早上5点钟就出现在南平的文体路上。在南平有关部门公布的“3・23”特大凶杀案见义勇为名单里，实验小学学生应某某的家长应长余、实验小学保卫人员陈仕营、实验小学教务处副主任甘贵平、城管大队副大队长游钦章等四人都名列其中。“但那位第一个赶到现场救出了3个孩子的环卫工人呢？为什么要漏掉她？”许多人都忍不住这样问。据有关人士介绍，认定见义勇为名单的依据是根据当时校门口的监控录像，――“刘瑞英没有出现在录像上。”<br />
<br />
　　“凶手要真冲过来，我死定了”<br />
<br />
　　3月23日7时25分，血案发生时，48岁环卫工人刘瑞英正在实小对面的路边打扫。在看到那残忍的一幕时，她说，自己的脑子“嗡”地一下炸开了，一时吓得不敢动弹。“那边站着三个小朋友，后来这三个小朋友中被刺死了两个，另一个小朋友很精，往弄堂里面跑掉了，我看到还有学生从坡下上来，想喊他们快跑，嘴张得老大，就是喊不出声！”这个“胆小”的女人喊不出声，却举着扫把冲了过去。<br />
<br />
　　刘瑞英把身边的三个小学生迅速地拨拉到了身后，举起扫把乱舞。她说，自己当时非常害怕，怕凶手拿着刀冲过来，“那时我的腿都在抖，上下牙齿在打战，凶手有刀，要是真的冲过来，我死定了”。这个连自己名字都不会写的人，时候是这么描述自己的心情的：“我都快50岁了，一条老命换3个小孩的命，很值。”<br />
<br />
　　“他要刺死我我也没办法了，反正我不让开”<br />
<br />
　　在发现嫌犯郑民生残忍地捅杀学生后，体育老师甘贵平第一件事情就是搬起身边的车子砸过去，但身边都是电动车，拎不动，他跑到路边小店，拿起拖把冲到了凶手面前。此时的刘瑞英像老母鸡一样张开着双臂，护卫着身后的三个孩子，和甘贵平一起，与凶手对峙着。刘瑞英说：“他就离我两米多远，手里拿着刀，他要刺死我我也没办法了，反正我不让开。”<br />
<br />
　　郑民生停止了砍人，与他们对峙着。甘贵平大声吼道：“放下刀，否则我打死你！”南平市城管大队副大队长游钦章突然扑向了嫌犯，甘贵平和陈仕营也先后扑了上去，合力将嫌犯制服。此时的刘瑞英气急了，“我用扫把拼命地敲打他的头”。<br />
<br />
　　刘瑞英原本住在南平乡下，后来进城当了清洁工，这个文化程度不高的人说：“我是队长，要以身作则了。”她去年刚被“晋升”为文体路环卫队队长。<br />
<br />
