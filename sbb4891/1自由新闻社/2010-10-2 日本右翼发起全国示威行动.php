　　<font color="RoyalBlue">前日本航空自卫队幕僚长田母神俊雄周六在集会上演讲，谴责中国在东海霸权（BBC中文网照片）</font><br />
<br />
　　<strong>日本几个右翼组织周六（10月2日）在全国多个城市发起了一个叫作“谴责中国侵略尖阁诸岛全国国民统一行动”的示威。</strong><br />
<br />
　　<strong>批评中日两国政府</strong><br />
<br />
　　BBC中文网记者在东京涩谷区周六下午举行的示威集会所见，约有800人参加，演讲的包括了前日本航空自卫队幕僚长（相当于日本空军司令）田母神俊雄、东京都衫并区区长、日本创新党党魁山田宏等社会名人和不少地方议会议员。<br />
<br />
　　他们批评首相菅直人的内阁，尤其是认为官房长官仙谷由人主导对中国的软弱外交，呼吁日本国民再不动员起来保护国土，尖阁诸岛（中国叫钓鱼岛）将被中国掠夺，北方四岛将永远被俄罗斯霸占等等，听众中不时爆发喝彩与掌声。田母神演讲中还谴责中国海军今年4月在东海的活动是谋求东海霸权等。<br />
<br />
　　<strong>少有的全国性行动</strong><br />
<br />
　　这次行动是由“加油日本、全国行动委员会”、“草莽全国地方议员会”、“日本李登辉之友会”、“台湾研究论坛”四个组织合作主办，除了东京以外，周六在名古屋也有集会。<br />
<br />
　　此外北海道的札幌、京都府京都市等18个地方城市也有不同形式的抗议活动。东京的集会分作下午和晚上两场，下午集会结束后还有示威游行。<br />
<br />
　　<img src="http://c.imagehost.org/0486/1_12.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　<font color="RoyalBlue">周六日本右翼组织在全国展开谴责中国的行动。图为在东京涩谷区的街头示威集会（BBC中文网照片）</font><br />
<br />
　　日本右翼组织也各种各样。今年早些时候，BBC中文网记者在东京池袋采访日本右翼组织抗议在那里建设新唐人街时，如果不是警察及时介入，记者差点被参加示威的右翼组织成员殴打。<br />
<br />
　　<strong>参加者不同以往</strong><br />
<br />
　　周六的主办组织文明很多，而且显然较有计划性，有专人维持秩序并在现场周边发放专门印刷的报纸《樱新闻》，现场采访的外国记者们基本都能不受言行恐吓。<br />
<br />
　　周六参加集会者最大的特点可说是年轻人显著较多，比以往记者采访右翼组织示威行动看到中老年人和蓝领为主的结构不同，被日本社会形容为政治冷漠的日本年轻人令人意外地约占了三分一。<br />
<br />
　　还有不少妇女和看起来像是知识分子的参加者旁听，现场气氛冷静，但从喝彩和掌声中可以感觉到日本社会对中国不满的情绪。中国政府日前已提醒访日旅游的国民小心、香港也传出一些取消访日预定的新闻。</div>

							
							
							
							
