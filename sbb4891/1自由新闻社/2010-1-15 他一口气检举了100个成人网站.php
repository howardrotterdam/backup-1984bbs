沉迷黄色网站，丢了工作毁了面容断了亲情；如今为了儿子，他决心改过――<br />
<br />
　　他一口气检举了100个成人网站<br />
<br />
　　本报记者 叶玉跃 本报通讯员 杨俊<br />
<br />
　　结满疤痕的双手，没有一丝血色；脸上的皮肤干燥又“坑坑洼洼”，湖北人老翁指着自己脸上手上烧伤留下的伤疤说：正因为沉迷于成人网站，他成了今天这个样子。<br />
<br />
　　老翁其实并不老，才28岁，但受过伤的面容，让他看起来比实际年龄大了很多。<br />
<br />
　　老翁下定决心要检举曾经让他沉迷和堕落的100个成人网站，希望用自己的遭遇和行动警示那些深受网络色情毒害的人们，远离色情网站。<br />
<br />
　　昨天下午，我们和老翁在他城北的出租房见了面。<br />
<br />
　　举报信中，附着100个成人网站的地址<br />
<br />
　　打开电脑，老翁先给我们看了一封检举信：<br />
<br />
　　“尊敬的网监局领导：您们好！我是一名来自浙江杭州的网络爱好者……20来岁，我就来到杭州……一次偶然，网吧朋友介绍我一个成人网站，在躁动的驱使下，我走向了红灯区。<br />
<br />
　　自那次以后，一发不可收拾，隔三差五就去泡网吧，逛休闲店，洗头房。一年下来，体重急剧下降，工作没有激情……在无尽的精神折磨和自责中，我暗下决心，选择了‘重生’。”<br />
<br />
　　举报信后紧跟着一个表格，100个色情网站的名称和地址清楚列了出来。老翁从中随便复制了一个地址，打开一个网页，里面，果然充斥着不堪入目的淫秽图片和字眼。<br />
<br />
　　“这些网站一般很难找到，他们都有特殊的代理服务器和链接……找这100个就花了十多天时间。”说着，老翁打开网页浏览器，打入一小串数字和字母，跳出一个色情网站的导航仪，色情网站、文学、图片、视频、论坛、交友……类别详细繁多，用老翁的话说，“要什么有什么”。<br />
<br />
　　看着这些闪着红红绿绿“露骨”图片的网站，老翁苦笑着摇了摇头，“我以前天天沉迷在里面，真是被这些东西给害惨了。”<br />
<br />
　　沉迷“黄”毒，让他毁了面容<br />
<br />
　　老翁第一次接触电脑只有13岁，从此对电脑产生了浓厚的兴趣。<br />
<br />
　　初中毕业后，17岁的老翁来到一家做美容产品生意的公司上班。一次偶然的机会，朋友介绍给老王一个网站，打开一看，屏幕上那赤裸裸的画面，让年轻的他热血沸腾。因为缺少足够的自制力，老翁走进了红灯区。从此，这种恶性循环在他身上一次次上演，他沉迷其中无法自拔。<br />
<br />
　　每天都在网吧泡着，日夜颠倒，整个人的精神很差。夏天的一个下午，老翁昏睡了一天一夜后，起床准备烧点吃的。“其实当时厨房里的煤气味已经很重了，估计是前一次烧完饭就忘了关煤气瓶，但是当时气温也低，脑子反应也慢，也就没闻出来……”一打火，整个厨房里突然喷发而出的大火把他烧晕了――头部和双手严重烧伤，经过7天的抢救，命是保住了，但脸上、嘴唇、耳朵留下了伤口，他已经没“脸”见人了。<br />
<br />
　　为儿子，决定检举黄色网站<br />
<br />
　　幸运的是，老翁的脸经过很多次脱皮之后，恢复得还可以。2004年老王来到杭州，同样做着美容产品的推销。<br />
<br />
　　但是一个人杭州呆了没多久，无聊的老翁打开自己的电脑又开始逛起了这些色情网站，一个月1000不到的工资几乎全部花到了休闲房里。<br />
<br />
　　几年下来，没有一点积蓄。<br />
<br />
　　2007年，老翁借了点钱，在小区里开了家理发店，并让有理发手艺的弟弟来店里上班。20岁出头的弟弟也沉迷到了色情网站的世界里。为此，老翁和弟弟吵了一架，两兄弟闹翻了，店也关门了。<br />
<br />
　　后来，老翁在自己的小房子里开起了网店，生意马马虎虎。<br />
<br />
　　今年，老翁把自己4岁的儿子带到了杭州上幼儿园。<br />
<br />
　　“现在的孩子都很机灵，虽然才4岁，电脑开机关机都懂，还会玩些小游戏……”老翁说，儿子在玩电脑的时候，那些色情广告经常会跳出来：“影响真的很不好，我已经受够了色情网站带来的毒害，不希望儿子也走自己的路。”<br />
<br />
　　老翁趁儿子回湖北老家的这段时间，把自己曾经经常光顾的色情网站全都收集起来，一共有100个，准备向网监局举报。<br />
<br />
