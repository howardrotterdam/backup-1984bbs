<br />
　　毛新宇　举行座谈会纪念母亲 <br />
<br />
　　《法制晚报》报道，全国政协委员、军事科学院战争理论和战略研究部正师职研究员毛新宇在两会期间表示，今年他准备了3个提案，其中重点关注中医药发展和教育。 <br />
<br />
　　毛新宇表示，母亲邵华逝世一周年纪念日可能会举行一个座谈会，他写了一些文章怀念她。 <br />
<br />
　　毛新宇还证实了新添了一个千金的消息，他表示，“去年8月份出生的，到现在7个月了。”女儿的名字叫毛甜甜。 <br />
<br />
　　朱和平　写书纪念爷爷奶奶 <br />
<br />
　　朱德嫡孙、全国政协委员、空军指挥学院副院长朱和平3月7日晚表示，“我们的老一辈打下了一个红色江山，建国60年我们就开始了一个新的起点。”今年国庆大阅兵肯定是建国以来最好的一次阅兵。 <br />
<br />
　　朱和平在谈到朱德时说，爷爷的一生就是戎马一生，老一辈没有过过幸福生活，整个就是在追求探索中，新中国成立之后年龄就很大了，等到了我们第三代时，才有了正常的家庭生活。别人看到老一辈都是打仗，没有什么家庭生活。而我从小在他们身边长大，实际上就见证了他们另外一方面，就是家庭生活。 <br />
<br />
　　朱和平写了两本书，《长征中的朱德》和《永久的记忆》。《长征中的朱德》写的是爷爷作为红军总司令长征的过程。《永久的记忆》就是围绕了一个情字，夫妻情、战友情、师生情等。 <br />
<br />
　　《长征中的朱德》采访了很多当事者，有很多都是老前辈讲给我的故事，很多当事人都去世了。写这本书用了两三年，但是积累了很多年。《永久的记忆》是我亲身经历的，不用查什么东西，只是核实。写了四五年，就是为了纪念爷爷奶奶。 <br />
<br />
　　被问及今年会否参加60年大庆的阅兵时，朱和平表示，现在才刚刚开始筹备，还早着呢，人还没定呢。他认为国产的都会亮相，歼10战斗机连航展都参加了。</div>

							
							
							
							
