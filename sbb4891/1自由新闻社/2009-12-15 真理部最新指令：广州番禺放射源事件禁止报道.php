																											<div id="postmessage_374471" class="t_msgfont">南方都市报 C 番禺钟村： 钴-60放射源被卡48天<br />
编者注：因南方都市报今早头版刊登文章：《广州番禺锺村：钴-60放射源被卡48天。》文章被下令禁止转载，现在所有网站都删除该篇报道。我们在此将被删除的文章公布。<br />
●当地一家辐照厂因机器故障致放射源没法回贮源井，机器人出动清障●事件未出现放射性物质泄漏，未造成环境污染，未造成人员辐射伤害什么是卡源?简单地说，卡源就是辐照工厂用来辐射灭菌的放射性钴-60源板，由于机器故障，辐照完毕之后没法照常回到储存装置中去，从而一直留在辐照室内 ，对辐照灭菌的物质持续辐射。<br />
这样的事故不幸发生在了广州番禺钟村。10月12日晚上9时多，位于广州番禺钟村的广州辐照技术研究开发中心，发生了卡源事件。西南某大学机器人项目组临 危受命，经过多番努力。终于在11月28日中午成功清障，让漂浮了整整48天的钴-6 0放射源成功降回到储存装置―――贮源井内去。<br />
据了解，经专业检测，本起故障“有惊无险”，未出现放射性物质泄漏，未造成环境污染，未造成人员辐射伤害。<br />
12月10日，环保部在广州番禺祈福酒店举行“全国辐照装置卡源处置现场会暨专项整治工作启动会”。与会专家和代表就近期河南杞县利民辐照厂、广东广州辐 照技术研究开发中心、河南中牟天宏辐照中心发生的卡源事件进行了讨论，分析了原因并总结了经验教训，研究避免卡源故障的对策。<br />
会议举办地祈福酒店马路正对面，番禺钟村市广路钟三路段13号，就是此次卡源事件发生的广州辐照中心，四周厚厚的水泥墙，站在围墙外可以看到院内郁郁葱葱的树木，把办公大楼包围得严严实实。其西北200米，广东工业大学商学院坐落于此。向西一街之隔，就是番禺最大的居民社区祁福新村，常住人口超过10万人。<br />
中心左右两边是一片繁荣的二手楼交易市场，生意兴隆的房屋中介店铺与辐照中心的安静形成强烈的反差。<br />
同样是卡源事件，今年8月，河南杞县发生了现代版的“杞人忧钴”事件，而在广州，与辐照技术研究开发中心一街之隔的祈福新、广东工业大学商学院等单 位人员，大多对此毫不知情。<br />
这48天时间到底发生过什么?<br />
名词解释<br />
钴-60：地下恶魔的放射性亲戚钴-60是金属钴(分子量为59)的放射性同位素，自然界一般不存在，通常以中子轰击金属钴制取。钴的拉丁名称cobaltum来源于德语的kobalt(kobold)，意即“地下恶魔”，这个“地下恶魔”其实是辉钴矿，由于其中含的砷会使工人中毒，故名。钴-60的半衰期为5.27年，放射性极强，它能使人患上血癌，人若是站在距离钴源5米的地方，5分钟即可毙命。<br />
卡源<br />
―――货柜门挡路，放射源回不去10月12日晚9时许，广州辐照中心工作人员将34个装满调味料、中药材等的货 柜通过6条悬链轨道运进辐照室进行辐照消毒。在移动货柜的过程中，因为操作不当，致使3号通道南侧倒数第二个货柜门脱出，向下垂开，刚好挡在放射源架和贮 源井(3米以上水深可完全屏蔽辐射)之间，导致源架无法下降回到源井中，造成卡源。<br />
10月14日下午，广东核与辐射安全监督站接到群众举报并向环保部核安全管理司报告，随后确认该中心发生卡源事件。<br />
10月15日，国务院、环保部、广东省、广州市有关领导在事件上报后第一时间对事件作出了批示，要求妥善处理此事。李克强副总理批示：“环保部要速派员赴现场指挥处置工作，并提供必要的专业力量和技术支持，严防出现环境污染。”汪洋书记批示：“尽快披露信息，防止误传造成不良影响。”当天，环保部、广东省、广州市相关监管机构人员也在第一时间赶到钟村卡源事件现场，紧急成立了以广州市副市长陈国担任组长的前方处置小组，下设协调指挥、信息发布、技术支持、辐射环境监测和工程实施5个专业组。<br />
10月15日晚，前方处置小组经过紧急磋商，初步形成事件解决方案和消息发布策略，要求“必须在环境保护部广东核与辐射安全监督站的监督下进行故障排除作业”，同时要求“讲究消息发布策略，做到不隐瞒，不宣扬；控制舆情发展，注意信息的搜集、整理和报告，防止传谣信谣”。<br />
同日，核安全管理司发文要求广州辐照中心“进一步查明辐照室内设备状况和事件原因，……按经审核的降源方案实施降源工作”。<br />
10月16日，省环保厅、市环保局网站和有关媒体发布了短消息，称12日广州辐 照中心发生卡源事件，“事件已经得到有效控制，放射源处于安全状态，故障排除工作正在科学有效地展开。经专业监测没有出现放射性物质泄漏，没有造成环境污染，没有造成对人员辐射伤害”。同天，西南某大学机器人组专家抵达卡源事件现 场。<br />
10月17日，经过对事故模拟和对辐照室情况进行分析，前方处置组确定了“打孔挂钩”的处理方案。即一面在辐照室打孔安装摄像头以期获取室内图像，一面紧 急联系曾经成功处置杞县卡源事件的西南某大学机器人研究组，用机器人进入辐照室排障。<br />
“这次卡源事件处理的难度，可以说中外罕见。”在12月10日举行的现场会上，有专家如是感慨。处置工作进展一直不顺利。因为放射源强度大、辐照室结构复杂，打孔之后一直没能取得很好的观察效果，无法了解卡源具体地点的情况，从而使机器人也难以接近源板。<br />
最坏的后果：10月17日确定的备用方案是，用水将整个辐照室灌满，从此封存。<br />
起火<br />
―――辐照室货物自燃，内墙变薄10月18日晚，按计划于17日晚9时运抵的机器人，由于航班飞机货舱过小，直到18日晚才抵达。<br />
10月19日，机器人第一次进入辐照室迷道清障。排除了出货迷道中400多公斤重的载货车，成功获取了辐照大厅内的部分图像资料。但由于辐照室的安全门是“弓”字形的迷道，机器人拖着手臂粗细的控制线缆进入迷道后，控制线缆很快被卡住，机器人被卡在迷道中，首次清障试验以失败告终。<br />
10月19日晚，更不幸的事情发生了。尽管有严密温度监控和室内降温措施，辐 照室内货物仍然发生自燃，引起大火。消防车赶来灭掉火之后，室内所有的货物和线缆等已几乎完全烧毁。<br />
“我们至今不知道起火的原因是什么，为防止货物受照自燃发生火灾，该公司一直在采取连续用水淋源架的方法对辐照室降温。”一位知情人士表示，可能是货 物中有聚乙烯板(着火点低)导致的。一位食品辐照方面专家告诉记者，食品等货物长期接受辐射，极易着火，“一个90万居里的辐射源，相当于1万瓦的电炉的发热 量”。<br />
“起火进一步增加了事件的处理难度，地面障碍物增多，而且过火后有新的货 柜发生向源架倾斜的情况，使事件处置时间一下增加了很多。”知情人士表示。事件处理完毕后发现，由于大火和灭火时，辐照室墙壁骤热骤冷，内墙混凝土大面积 脱落，原来2.2米左右厚的墙壁只剩下2米左右。<br />
10月21日，广州两家媒体分别刊登《卡源事件排障已取得有效进展》、《卡源事件排障正在进行，未对环境人员造成影响》的新闻通稿。称“由于部分货物仍滞 留其中，温度较高，19日21时许出现辐照室内货物冒烟，采取喷淋水措施后有效控制了烟雾。经广东省环境监测中心监测，未监测到有毒有害物质，没有发生环境污染，亦没有人员受伤害。”清障―排爆机器人也被卡，清障机器人出动据介绍，由于源板卡住的具体位置的情况一直没有得到清晰的图像，大火又导致地面障碍物进一步增多，处置工作的第一步就是清障，使机器人能进入室内获取图像，也为后期处理时机器人进入室内解决卡源问题创造条件。<br />
机器人被卡在迷道中后，为了防止再次出现机器人出不来的情况，技术人员根据现场需要，设计了“空地一体机器人组”，紧急开发了两台悬挂式轨道机器人( 即悬链轨道小车)和一台地面排障机器人。利用辐照室本身的货物悬链轨道(即屋顶 下挂载货物的轨道)，让一个悬挂式轨道机器人通过悬链轨道进入迷道，让它牵引地面机器人的控制线缆以防被卡。“这条悬链轨道在最后成功处置卡源事件中发挥 了重要的作用，空地一体也是一个创新，要不清障机器人根本进不去。”知情人士说。<br />
10月22日，广州市南沙区公安分局一台排爆机器人被调来现场，进入迷道协助清障和观察。当晚，被卡的机器人被拖出迷道进行检修。<br />
10月26日，同城媒体再次发布题为《卡源故障排除工作有序推进》通稿消息，称：“广州辐照技术研究中心辐照室内发生卡源事件后，有关部门和专家积极、稳 妥组织处置，故障排除各项工作正按方案周密、有序地推进。”10月27日，排爆机器人成功进入辐照室，获取了较为清晰的辐照室内具体情况录像，但随后也被卡在辐照室内。当天，专家组就如何解决机器人前进中遇到的障碍清除问题进行了技术讨论。迷道内掉落的混凝土和电线等杂物很多，清障工作难 度很大，现有的机器人难以完成此任务。28日，技术人员对电动悬链轨道小车进行改装，在其上加装机械臂，经调适后，开展货柜清理工作。<br />
10月30日，电动悬链轨道小进入货物入口迷道，利用机械臂拉动迷道内的一个货柜至迷道内口(即靠近辐照室一端)附近。31日上午，辐照室外人员在辐射监测组 监护下，人工将货柜拉出。“这时迷道内的货柜才被清空，但室内起火后的障碍物、地面原有的轨道等影响机器人进入的障碍物仍需要继续处置”。<br />
降源―――钴-60放射源成功回井“取得室内清晰的图像是成功处理问题的关键，事件处理进展缓慢也是因为室内环境复杂，一直无法取得清晰图像。”知情人表示。在机器人持续清障过程中，打孔组也一直在忙碌，通过寻找恰当的位置和合适的摄像头、灯光搭配，以期获取清晰图像。<br />
11月14日，通过多个孔道协同，终于获取辐照室内整体的清晰图像，但源板位置仍有障碍物遮挡。15日取得更清晰图像，确定卡源故障基本上是“因3号通道南侧倒数第二个货柜门脱出造成”。有关人员称，“为了取得室内的清晰图像，前后有100多个摄像头被辐射坏”。<br />
11月24日，清障机器人成功将源板与新的钢丝绳对接上，源板被固定好。“固定源板是降源的关键，只有源板不再滑动，才能保证安全降源。”参与处理的一位专家表示，带有挂钩的钢丝绳成功与源板挂接，此为突破性进展。<br />
当天，处置组发布新闻称，“据市环保局透露，广州辐照技术研究中心卡源排障工作在处置组的努力下，已实现第一阶段处置目标，继续稳妥推进。广东省环境辐射监测中心定时监测结果表明周边空气和水环境无变化，事件未对环境造成影响。”11月26日，机器人将阻挡源板下降的货柜与挂钩接上，阻挡源板的货柜栅栏被拉开，贮源水井和源板之间的障碍物都被清空，“这时才真正具备降源条件”，知情人士透露。<br />
11月28日，经过48天艰苦攻关，现场处置组成功排除广州辐照技术研发中心卡源故障，将钴-60放射源顺利回降到贮源水井中。<br />
11月29日，广东省应急办公室网站发布消息：“被卡放射源安全降入储源井内 ，处置工作取得圆满成功。28日下午开始，处置人员继续在处置专家组的指导下对 现场进行勘察和后续清理工作”。<br />
经省辐射环境监测中心监测，辐照室内及周边环境辐射剂量水平全部处于安全范围。整个事故处置过程，没有发生放射性物质泄漏，没有造成环境辐射污染，没 有造成人员伤害，没有造成社会影响，社会舆情平稳。<br />
反应<br />
周边居民对卡源事件一无所知广州市政府部门：不隐瞒， 不宣扬，防止传谣信谣 本报讯广州卡源事件发生以来，有关部门一直在防止出现“群众误传、媒体网络炒作现象”，以免出现杞县类似的情形。<br />
10月15日，事故处理组明确强调：“信息发布由广州市政府牵头负责，广东省环境保护厅配合，任何单位和个人不得擅自对外发布消息”，并要求“广州市政府部门应尽快通过相应渠道发布消息，讲究策略，做到不隐瞒，不宣扬；控制舆情发展，注意信息的搜集、整理和报告，防止传谣信谣”。<br />
12月11日，记者拨通广州辐照中心主任彭志刚电话，他表示，现在不方便就此事接受采访，请记者谅解；记者与省环保厅联系，得到的信息是，事情已经处理完毕，不方便发表意见。<br />
与此相对应的是，广州辐照中心周边的大多数居民都不知道这个中心是干什么的，12月11日，记者在广州辐照中心周边走访时发现，尽管与其一墙之隔，但周边的商铺、居民和大学生多不知道这个“神秘”的单位的性质。<br />
记者在走访时发现，对于两个月前这里发生的卡源事件，以及事件处理情况。<br />
周边住户也多不了解。“没听说啊，要是有辐射，我们学校早放假了!”广东工业大学商学院校园内一位大学生说。“有辐射泄漏，这里的房子还能卖得这么好?房主都是傻瓜啊!”中原地产的一位业务员说。<br />
