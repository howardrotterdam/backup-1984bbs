专家：卡源不会发生核爆炸或核泄漏 <br />
放射源长时间直接接触空气和水才会发生爆炸或泄漏，而卡源只是<br />
发生在封闭的辐照室中<br />
本报讯广州市辐照技术研究开发中心卡源事件11月28日得到成功处置，监测结果发现，没有发生放射性物质泄漏，没有造成环境辐射污染，没有造成人员 伤害。卡源问题如果得不到解决，或者解决的时间拖了很长，是否会造成环境污染甚至辐射泄漏呢?<br />
钴-60能量会逐渐减少<br />
据一名不愿透露姓名的辐照企业技术人员透露，钴-60作为一种放射性物质，从它装置进源板的时候，其自身能量就不断衰减，5年多以后即衰减掉一半，时间越久，其放射性越低。“理论上讲，源板一直卡在辐照室也没关系，随着时间过去，其放射性会逐渐衰退，直至消失”。<br />
“钴-60只会发生能量逐渐减少的核衰变，不会发生类似核武器、核电站用的放射源那样的能量瞬间增加的核裂变”，因此，卡源事故不会发生核爆或者核电站泄漏类似的情况。<br />
辐照企业进行辐照作业都有专门的辐照室，是墙体厚度超过2米的钢筋混凝土结构，加上安全门等装置。平时，是不会发生核辐射从辐照室内泄漏出来的。“卡源事故中，最害怕的就是起火，如果大火燃烧导致墙体温度升高，灭火时又要加入大量冷水降温，骤热骤冷可能导致墙体破裂，但两米多厚的墙，一般是不会破裂的。”据知情人士透露，广州辐照中心发生大火时，广州某建筑设计院曾对辐照室墙体进行评估，证明不会发生破裂。<br />
此次卡源事件的最坏结果，也就是10月17日确定的备用方案：用水将整个辐照室灌满，从此封存。<br />
长期接触空气和水才会核污染火灾的另一危害是可能烧坏放射源的防护层，导致放射源直接接触空气和水。<br />
“一般说来，源板在辐照室内都是安全的、不会发生泄漏的。钴-60都是先装进源架的套管中，然后再在外面加上两层不锈钢防护膜，可以耐1500℃的高温。”因此，即便发生火灾，也不至于将防护层烧坏导致放射源直接接触空气和水。“只有放射源直接、长时间接触空气和水，才会造成核污染和核泄漏”。<br />
据国际原子能机构统计，食品辐照发生操作者受到超剂量辐射的事故中，违规 操作是主要的原因。辐照室墙体、货物运送装置和安全门，将发生辐射危害的可能降到了很低，只要人员不直接进入辐照室，基本不会出现放射性物质泄漏、造成环 境污染和人员辐射伤害等情况。但他们同时也强调，“必须有专门的机构对食品辐 照的放射源进行监管”。<br />
“我国迄今未发生过泄漏事故”“卡源事件其实就是一件安全生产事件，造成像杞县那样的恐慌，其实也与我们对这个产业的顾虑太多有关。”据专家介绍，辐照产业在国内外早有广泛的应用，我国也早在上世纪80年代就开始引进了辐照装置，应用于食品、药品和医疗器械等的消毒，“迄今没有发生过泄漏事故”。<br />
“但直到今年杞县发生卡源事件之前，公众对此几乎毫无了解，辐照处理过的食品大多也未按国际标准加上辐照标志，因此造成谈核色变等问题。建议加强辐照产业的公众认知度，相关知识普及了，政府也不会如此被动，居民的知情权也得到了保障。”一位不愿透露姓名的辐照企业负责人说。<br />
处理专家：钟村卡源处理难度国际罕见广州辐照中心装源量达到９０多万居里，是河南杞县卡源事故的6.5倍，危险性更大今年6月河南杞县利民辐照厂卡源故障和广州辐照技术研究开发中心“1 0&#8226;12”卡源故障，存在诸多相似之处：都是面向社会的辐照企业，都利用放射源对食品、药品、农产品等进行辐射灭菌消毒；都是因故障，导致放射源“钴-60”未能正常返回储源井中，造成卡源；处理过程中均发生了货物起火等情况；也都是西南某大学机器人项目组临危受命，成功应急处理，开创了国内采用机器人处置卡源故障的先河。经专业检测，两起故障都“有惊无险”，未出现放射性物质泄漏，未造成环境污染，未造成人员辐射伤害。<br />
但两地情况也不尽一样。河南杞县卡源故障处理时，由于谣言引起“杞人忧钴 ”事件，受国内外极大关注，舆论压力极大，西南某大学机器人项目组曾6天6夜连 续奋战，加快进度处理。而此次广州卡源排障，舆论压力小，时间相对充裕。然而，从技术上看，广州“10&#8226;12”卡源故障的处理难度，远远大过河南杞县卡源故障。<br />
知情人士介绍，河南杞县利民辐照厂辐照室内，放射源装源量为14万居里，而广州辐照中心的装源量达到90多万居里，辐照剂量更高，危险性更大。<br />
广州卡源故障现场环境更为复杂。据介绍，进出辐照室的迷道拐弯比河南的多，辐照室内障碍物也更多，有34个装载货物的大货柜；由于放射剂量高，辐照室内 还发生自燃，水泥块、电线被烧不断脱落，室内照明设施也一度被毁，技术组想获 得室内图像资料较为困难。<br />
此次广州卡源故障处理时，因辐照室内地形太过复杂，一台加上设备重达300 多公斤的机器人被卡在通道，更增加了排障难度；而在河南杞县处理卡源故障时，也曾有一台美国机器人被卡在辐照室内，但那台只有90多公斤重，且室内地形没广州此次复杂，较易处理。<br />
在12月份于广州举行的全国辐照装置卡源处置现场会上，有业内权威专家称，广州卡源故障的处理技术难度之大，在国际上也属罕见。<br />
知多D<br />
常见“辐照食品”有哪些?<br />
常见的“辐照食品”有哪些呢?据了解，洋葱粉、八角粉、虾粉、青葱、辣椒粉、蒜粉、虾仁等脱水产品，月饼、袋装肉制品(如火腿肠)、果脯等延长货架期的产品，冻鱿鱼、冻虾仁、冻蟹肉、冻蛙腿等冷冻产品，减肥茶、洋参、花粉、灵芝制品、袋泡茶、口服美容保健食品，方便面等方便食品都属于“辐照食品”。<br />
食品辐照技术是一种利用放射性元素的辐射杀菌技术。辐照加工能帮助保存食物，消除危害全球人类健康的食源性疾病，使食物更安全，并延长食品的货架期。<br />
辐照能杀死细菌、酵菌、酵母菌，这些微生物能导致新鲜食物、水果和蔬菜等腐烂 变质。此外，辐照还能杀死食品中的昆虫以及它们的卵及幼虫。<br />
世界卫生组织、联合国原子能机构以及联合国粮农组织早在1980年就已明确宣布：辐射剂量在10千戈瑞(1戈瑞1焦耳/千克)以下的食品没有毒理学上的危险。我国也于1998年出台了《辐照食品卫生管理办法》，认为辐照处理的食物是安全的，除了对维生素C的含量有所影响外，对食物其他品质没有影响。<br />
今年8月，卫生部食品安全综合协调与卫生监督局食品安全评估预警处处长李泰然指出，应用规定剂量辐照的食品是安全的。<br />
广东省辐照企业一览<br />
广州辐照技术研究开发中心是我国自行设计和建造的全国最大的商业性辐照中心之一。该中心拥有一个设计最大装源量为200万居里的钴-60伽玛辐照装置。<br />
地址：广州番禺钟村市广路钟三路段13号广州辐锐高能技术有限公司是广州市科技局属下的一个高新技术企业。拥有设计装源100万居里钴-60伽玛 辐照装置。<br />
地址：广州市南沙区大涌村工业五路深圳市金鹏源辐照技术有限公司该公司设计放射源容量为400万居里，目前该装置已累计装源385万居里，钴源活度达到185万居里，是我国规模最大、装源量最高、加工能力最强的商业化辐照装置。<br />
地址：深圳市罗湖区布心东盛路68号广州华大生物科技有限公司是华南农业大学下属的“产、学、研”一体化公司。主要经营生物医药、食品、化妆品、农产品等的辐照灭菌与保鲜、辐射技术的研究与开发。设计装源容量40 0万居里，今年投产。<br />
地址：广州市科学城经济技术开发区开泰大道伴河路佛山塑料集团股份有限公司来保利分公司是佛山塑料股份集团有限公司经纬分公司下属的独资分公司。是佛山唯一一家辐照企业，主要是进行塑料辐照改性。<br />
