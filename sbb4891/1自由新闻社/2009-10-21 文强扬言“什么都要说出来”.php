																											<div id="postmessage_292694" class="t_msgfont">东方木 　发表时间：2009-10-21 00:46:48<br />
随着重庆打黑运动中第一批四大涉黑团伙（杨天庆案、刘钟永案、李义案、谢才萍案）庭审完毕，原重庆市公安局原副局长文强涉黑案即将进入诉讼程序。<br />
现被异地关押在贵州的文强，对待专案组人员审问时态度已经发生了180度的变化。他最初对专案组审讯人员曾扬言说：“你们不判我死刑就罢了，要是判了我死刑，没那么便宜，我什么都要说出来，大家就等着一起死吧！”此话是什么意思？<br />
人之将死，也许文强什么都不在乎了，“破釜沉舟”也许就是他真正的性格，何况他在公安局刑侦领导岗位上干了那么多年。对于他这种“久经沙场”的老刑侦来说，当真正的死亡来临时，也许他说的这番话才是最真实的。正因为他这句话的真实，尽管有很多人会睡不着觉，但笔者还是好奇，他最初对专案组人员说的那番话是否有所指？<br />
据悉，文强刚被羁押时，面对专案组人员的审问，态度十分强硬，拒绝交代，他甚至向办案人员叫嚣：“别想通过审问从我口中获得更多的东西！你们审问我的方法，是我以前审问罪犯的方法！”对于身陷囹圄的他还敢如此放肆，在感受他张狂的同时，不得不让人从另一个角度去思考问题，那就是在他张狂的背后，向我们吐露一个玄机，即利用这种方式向某些人传递一种信息：我文强是靠得住的，是硬骨头，不会出卖你的。<br />
那么这个“某些人”是谁呢？从文强的个人资料来看，他1972年参加工作，到2008年7月出任重庆市司法局局长，这其间他先后担任过巴县公安局副局长、政法委副书记兼公安局副局长，巴县县委常委、副书记，四川省重庆市公安局任副局长。重庆直辖后，他担任重庆市公安局副局长，2000年11月被提任正厅局级侦察员，2003年任重庆市公安局党委副书记。在他的仕途上，提拔他的领导肯定不少，那么又是谁一步又一步把文强提拔上来的？<br />
对于文强被谁提拔的问题，目前仍是一个“谜”，尽管网上有这样或那样的猜测。<br />
早在2002年，因文强的腐败问题和他与黑帮过从甚密的关系，原全国人大代表、82岁高龄的重庆市人民政府参事雷亨顺就曾感慨地说，“文强的问题历届政协委员和人大代表反映很多，为什么动不了?”答案也许只有一个，那就是文强并不是一个人在腐败，他的身后有靠山，有后台，而且必定有一个更大、更强、更硬的靠山和后台！<br />
官场的交易是一条深不可测的利益链，而且像网络似的纵横交错，文强也许只是这条利益链中的一个小小环节。这个环节要是出了问题，大不了把他砍掉重新链接。但如果文强真的能拿出拼死的勇气，纵横交错的利益链就会一节一节地浮出水面，问题是政府有没有决心顺着文强的这个环节深究下去。<br />
据有关报道，文强的资产已达9位数，有8处房产，价值3000万元。报道中说房产“位于重庆武隆县仙女山国家森林公园，是占地20多亩的双栋别墅。当地一直流传着一种说法，即：文强买地没有花1分钱，是武隆县一位主要领导白送的；文强建房子也没有花1分钱，是建筑商替他免费建好的。”可见，在文强的身后，已形成一条完整的官官勾结的黑色链条。<br />
重庆打黑已取得了阶段性胜利，文强作为黑恶势力的“最大保护伞“也被掀翻落马，但遗憾的是，除了与文强有染的部分官员栽倒外，文强身后那个更大、更强、更硬的靠山却始终没有现形。能不能继续深挖下去，能不能在打黑运动之后再刮一次强烈的反腐风暴，把文强的后台也给掀翻？这倒是广大人民所期盼的。（东方木首发博客日报）<br />
