													<h2>13岁男孩被挂屋梁上离奇死亡 身穿红裙脚捆秤砣</h2>
						
						
																											<div id="postmessage_317340" class="t_msgfont">13岁男孩被挂屋梁上离奇死亡 身穿红裙脚捆秤砣<br />
2009-11-07 07:00:00　来源: 华龙网-重庆晨报(重庆)<br />
<a href="http://news.163.com/09/1107/07/5NGGAMEG000120GR.html" target="_blank">http://news.163.com/09/1107/07/5NGGAMEG000120GR.html</a><br />
<br />
核心提示：11月5日，54岁的农民工匡某从重庆江北赶回巴南区家中发现一片狼藉，13岁儿子穿着大红色的裙子，两脚之间挂了一个大秤砣，双手被捆着挂在了屋梁上，双脚离地几厘米，全身冰凉，早已死亡。该父亲称儿子生前无怪癖，不相信自杀，警方目前已介入调查。<br />
<br />
<br />
男孩生前无怪癖，家长不相信系自杀。<br />
<br />
<br />
家属痛不欲生<br />
<br />
重庆晨报11月7日报道 昨天，死者父亲匡纪绿说，刑警和法医对儿子有三个不理解：<br />
<br />
1、男孩为何穿着红裙子、游泳衣？<br />
<br />
2、死者额头前的小针孔从何而来？<br />
<br />
3、死者双手、双脚有非常专业的打结。<br />
<br />
5日中午12时许，54岁的农民工匡纪绿从江北赶回巴南区东泉镇双星村高石坎，为上住读的儿子送钱。家里正门、侧门紧闭，平时从来不开的后门却虚掩着。从后门进去，眼前一幕让他大惊失色：儿子身穿红色的花裙子，双手、双脚被绳子结结实实地捆着，脚上还吊着一个大秤砣，双手被挂在屋梁上，早已死亡。<br />
<br />
男孩家中离奇死亡<br />
<br />
匡志均是匡纪绿的独子，是东泉中学七年级二班的学生，死时刚13岁零13天。<br />
<br />
昨天，记者闻讯赶到死者家中。匡志均的遗体摆在堂屋正中，身上盖着一床红被子。除了一间正屋，还有两间偏房和一间灶屋。后门就在灶屋里。孩子平时在楼下正屋大床上睡觉，床旁的屋梁，竟是孩子的最后归宿。<br />
<br />
屋里地上到处是衣服和杂物。孩子用过的课本、作业本，散乱地放在床上、桌上。两包方便面，吃了一包。电子表、书包、计算器、手机、光盘等孩子的遗物留在床上。书包里还有32.5元钱。<br />
<br />
匡纪绿说，警方和法医已在5日晚对儿子进行了解剖。因此，孩子从头部到腹部，都被线缝着。<br />
<br />
记者揭开红被子看见，匡志均遗体额头前有一个小孔和不重的外伤，大腿、双手、两肋、双脚裸部上方，都有极深的勒痕。此外没有任何伤口。<br />
<br />
死者身穿红裙子<br />
<br />
匡纪绿告诉记者，前几天他的手机坏了，跟儿子联系不上，5日中午12点多钟，他回家为儿子送饭钱。平时进出的大门和侧门却怎么都打不开，他绕到后门，后门虚掩着，轻轻一推门就开了。<br />
<br />
家里一片狼藉，娃儿的衣服丢得到处都是。走进正屋，灯还开着，匡纪绿一眼便看见，儿子穿着大红色的裙子，裙子上还别着白花，全身被绳子扎扎实实地捆着，两脚之间，挂了一个大秤砣。双手被捆着挂在了屋梁上，双脚离地几厘米，旁边一个长椅被推翻在地，儿子全身冰凉，早已死亡。<br />
<br />
匡纪绿41岁时才得了这个儿子。眼前的情景让他傻眼了，他一屁股坐在地上，大哭起来。<br />
<br />
家人不相信孩自杀<br />
<br />
很快，巴南区刑侦队的刑警赶到。5日晚，市公安局的刑警和法医也来了。<br />
<br />
匡纪绿说，刑警把儿子从屋梁上放下来，脱去他的红裙子，发现儿子贴身竟然穿着他堂姐的游泳衣，儿子自己的衣服一件没穿。<br />
<br />
匡纪绿说，法医告诉他，初步判断，儿子是在48小时内死亡的，也就是11月3-4日。儿子身上，除了多处深深的勒痕外，几乎没有外伤。法医带走了儿子的内脏等物，回城里解剖。<br />
<br />
昨天，孩子的妈妈辜登会在记者面前，已没了泪水，她说，自己和丈夫都在江北区打工，家里就儿子一人。平时，每个周末儿子都回江北和他们在一起。因为10月24日儿子回来时，他们给了儿子几百元作饭钱、资料费等，儿子就说下周11月1日不回江北了，要自己回农村老屋。<br />
<br />
辜登会说，平时，家里的后门从来不开，都用两块大木板挡着，外加一根钢筋。儿子死后，大门、侧门关着，后门开了，两块大木板和钢筋被放在门的左右两旁。<br />
<br />
匡纪绿说，儿子与他们最后的日子里，他一点都没有异常的表现。“我们不相信他会自杀。”<br />
<br />
孩子生前没有怪癖<br />
<br />
匡志均邻居王伦琴对记者说，匡纪绿全家都很老实，平时对人也友善，从来不和别人发生纠纷，匡志均平时少与人说话，害羞得很。从不主动招呼人。娃儿啷个就突然死了，全村人都觉得太怪了。<br />
<br />
70岁的邻居邓先碧说，匡志均平时贪玩，成绩不太好，但人和他妈、老汉一样，老实得很，从不招惹哪个。以前也从没发现他有穿女孩衣物的怪癖。<br />
<br />
一周没有到校上课<br />
<br />
家长质疑学校责任<br />
<br />
匡纪绿说，儿子从10月30日放学后，就没有返校了。整整一周的时间，学校为啥不通知家长？如果学校及时与家长取得联系，儿子可能不会死。<br />
<br />
在东泉中学，匡志均所在七年级二班同学告诉记者，匡志均平时不爱说话，人老实，成绩在班上中偏下。上周星期五放学前，他一切正常。但是，匡志均周末放学回家后，就没有再来学校，一周没有到校。<br />
<br />
昨天，记者以匡志均表姐身份来到东泉中学。匡志均班主任吉老师说，星期天返校后，她发现匡志均没有返校，立即向学校刘校长作了汇报。校长说马上和家长取得联系。但自己多次和匡志均父亲打电话，但匡志均父亲手机坏了，没有联系上。<br />
<br />
甘校长说，这些天，因为流感，学校有30多个学生没有到校上课。学校不可能因为一个学生不来，就要派人到家里去看。<br />
<br />
甘校长说，家长可以通过法院起诉，到时候，学校该负什么责任，就负什么责任。<br />
