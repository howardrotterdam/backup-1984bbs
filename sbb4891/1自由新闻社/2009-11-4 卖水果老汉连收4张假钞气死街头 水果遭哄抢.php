几张假钞，一条人命，一场人间悲剧。因一星期内连续收到4张假钞，62岁的小贩气急攻心倒在了水果摊上。而令人不耻的是，就在小贩猝死倒地后，留下的700多公斤水果被一些路人在半个小时内连买带拿一扫而空。这是10月31日上午发生在娄底市金谷市场的一幕。<br />
<br />
&nbsp; &nbsp; ■记者 王石磊 通讯员 郭国权<br />
<br />
&nbsp; &nbsp; 现场：老汉猝死，水果贱卖<br />
<br />
&nbsp; &nbsp; 10月31日上午，娄底市金谷市场传来阵阵恸哭声，死者李谋辉的家人悲痛欲绝。记者在现场看到，只见老人已经躺在木架上，身体全部用衣服覆盖，在其身边烧了一堆冥钱。<br />
<br />
&nbsp; &nbsp; 李谋辉的家人急于准备安葬老人的后事，把没有卖掉的2辆三轮车上的柑橘、苹果等水果作2毛钱一斤低价处理，吸引许多群众马上来抢购。围观的群众看到这么便宜的水果，加上主人不在，也开始哄抢死者水果车上的柑橘。有的好心人给了一二元钱，更多的人则是看到没有人问要钱，拿起水果就走人。半个小时，连买带拿大约700多公斤的水果就被一扫而空。 <br />
<br />
&nbsp; &nbsp; 起因：一周收4张假钞还被人砸<br />
<br />
&nbsp; &nbsp; “父亲是收到假钞活活气死的！”据李谋辉的儿子李建军介绍，父亲是涟源荷塘人，今年62岁，有一儿一女，在金谷市场做水果生意已有几年，父亲为人比较随和，人际关系比较好。老人在世时身体本身不是很好，有高血压，平时脾气比较大，个性比较强。<br />
<br />
&nbsp; &nbsp; 前几天，有人拿20元假钞在李谋辉那里买水果，被他发现，接着就发生了口角争执。当时，买水果的人将李谋辉的后脑勺砸出血，李谋辉在附近医院看了病后，买水果的人赔偿了3000元医药费就离开了。<br />
<br />
&nbsp; &nbsp; 10月29日上午，又有一个年轻人拿了一张20元钱的假钞跟李谋辉买了2个苹果。李建军介绍说：“当时父亲没有发现是假钞，过后给我检验后才知道是假钞。父亲马上把买苹果的年轻人追到，年轻人被追到了，将我父亲推倒就跑。在附近好心群众的帮助下我们才把那个年轻人抓获，并送到金谷市场警务室。”<br />
<br />
&nbsp; &nbsp; 10月31日上午，李谋辉又在水果摊上收到一张20元的假钞。“父亲因为年老了眼睛不好，在一个星期内一连收了4张假钞，其中1张100元的，3张20元的，20元是FA6开头的。我们卖一箱香蕉才赚5元钱，收了几张假钞，这几天就等于白干，因此我父亲对此事耿耿于怀，可能是气急攻心，父亲当场晕倒在地就再也没有醒来。”李建军说：“我想就是假钞害死我父亲的！”<br />
