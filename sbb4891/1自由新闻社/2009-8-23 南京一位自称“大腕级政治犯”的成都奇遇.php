													<h2>南京一位自称“大腕级政治犯”的成都奇遇</h2>
						
						
																											<div id="postmessage_212033" class="t_msgfont">成&nbsp;&nbsp;都&nbsp;&nbsp;奇&nbsp;&nbsp;遇<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;自三年前心脏手术后，一直未出远门。近年来成都老友R君多次建议我，趁手脚还算灵便不妨外出走走，一则换换空气找点新鲜，再者借此机会好好聚聚，经老友再三蹿促，加上自已久静思动，遂于8月上旬b身赴西南。8月7日先飞贵阳看望年近九旬老岳母，复于8月10日飞抵成都。<br />
&nbsp; &nbsp;&nbsp;&nbsp;老友R君是办事极认真之人，考虑我爬楼及行走多有不适，在我抵蓉之前，特地通过Google卫星地图为我找了四家离他府上最近的宾馆供我选择，最后挑中了距离他家0.36公里的成都法官接待中心。该处原是成都中法内部招待所，近年来对外接客。<br />
&nbsp; &nbsp;&nbsp;&nbsp;自思这辈子同公检法打了几十年交道，而以旅客身份下榻法院开办的宾馆，这倒是头一遭，心中未免有三分好奇。及至入住后发现，这家宾馆无论硬件设施或服务质量都相当不错，尤其是二楼自助餐，品种繁多、价廉物美，几位服务员小MM个个笑容可掬，待人亲切。最令我满意的是夜晚极为安宁，通霄无人打扰。记得四年前我在成都另一家宾馆住宿时，什么条件都好，就是“小姐”打扰过多，有次居然敲开门后硬要强行进入。<br />
&nbsp; &nbsp; 这法官接待中心虽然开门接客了，住店的却很少，几天下来我几乎没看到什么旅客模样的人，每天在餐厅见到的全是老中青各色公检法。独自一人来到外地，置身在这种环境中无疑会大大增加安全感，如果不是后来发生了点小意外的话，肯定我还会多住上些日子。<br />
&nbsp; &nbsp; 抵达成都次日，R君侄儿F先生亲自驾车带我玩了一天，晚间又逛了锦里夜市。F前些年炒股和炒期货很是赚了些钱，腰包一鼓自然而然就成了大款阔佬们云集的某些去处常客，大约是为了显摆一下，夜宵时突然提出，成都两家名媛休闲会所的小姐都是一流佳丽，明天上午要带我去消遣一天，好好领略一番倚红偎翠滋味，见我笑辞不应，连称已联系好三位绝色妙龄女郎，且已付过订金，只等我们明日一行三人前往潇洒。见F君如此盛情，不忍拂却人家这般美意，遂允之。<br />
&nbsp; &nbsp; 次日上午七时许下楼早餐，吃饭间，邻桌有三位正在交谈，称今日上午成都中级法院要审判一名政治犯，可能会很热闹。其中一人问审判是否公开，骋荒晁杲洗笳叽鸸开审理。接着声音忽然低了下去，以至下面内容无法听到。<br />
审政治犯？我一听登时来了兴趣！别的不说，单就“政治犯”这三个字，即足令我怦然心动。<br />
&nbsp; &nbsp; 自1949年以来，在我国的任何法律辞典中是找不到政治犯这个词的，即以我这个比政治犯还政治犯的大腕级政治犯而言，当年的头衔也不过是“现行反革命”，而“反革命”按中国的说法属于刑事犯罪。如今在成都居然出了政治犯，M非咄咄怪事？我侧脸盯了一眼邻桌身着便装的三位，以我几十年的经验判断，那身份绝对非公检法人员莫属。话由这三位道出，谅必不会有假。<br />
&nbsp; &nbsp; 按中国现行法律，凡公开审理的案件，年满18岁的公民都可以列席旁听，我决意去看看这审政治犯到底是怎么回事？<br />
&nbsp; &nbsp; 匆匆结束早餐后，下到一楼总台向值班小姐请教成都中法地址，总台小姐笑着告诉我出门左拐50米即至―――弄了半天，我下榻处原来就在成都中法大楼之内。<br />
&nbsp; &nbsp; 与R君正待出门，F先生忽然驾车而至，称先带我们去吃早茶，然后再去休闲会所潇洒。我赶忙告知情况有变，找小姐一事看来得另择时日，同时把准备去旁听政治犯受审一事告诉了他。F一听当即连连摆手，说刚才从法院门前经过时，外面己聚集了好多人，并且有大量警察在场，弄的不好今天要出事。当时他摇下车窗问一看热闹的究竟咋回事？那人说今天这里审一个姓汤的叫啥子仁的，这家伙专门在网上写文章同政府作对，据说北京还来了几位大律师，云云。F叫我和R君千万莫去，以我们偌大年岁，这种是非之地还是远离为妙。他不说也罢，经他如此一介绍，更激发了我的兴趣，当即含笑相告：“我这人历来参照D的民主集中制原则办事，下级服从上级，小头服从大头，但凡小头和大头出现矛盾时，毫无疑问得以大头为重，今天我只能选择去法院，不得不暂时放弃休闲会所了。”见我主意已定，F只好怏怏作罢。大约是怨责我有些不识抬举，直到离开成都时再也没见过他，去休闲会所寻芳之事自然也成了泡影。<br />
&nbsp; &nbsp; 出门左行20米是十字路口，眼光一扫便发现气氛不对，首先是东南西北四角都停着不少警车，每辆车旁均有数名警员站立，那架势很象等待某重要人物通过道口。沿左侧法院方向望去，只见法院门前、周围以及街对面都站有不少警察，另外还有不少转来转去的闲人，那神态一看无疑都是便衣；再就是法院围墙下席地坐了一溜排人，有的在摆弄笔记，有的在打手机，那模样肯定不是沿街揽活的农民工，十有八九都是些奔今天法院审判而来的有关人士。<br />
&nbsp; &nbsp; 行至法院大门时，发现好多人正从大门左侧一小门朝内涌，上前一看是进入法院的安全检查通道，我和R君排了不少时间队才通过安检。法院安检非常认真，除对进入者的随身物品彻底检查外，搜身时每人还必须高举双手作投降状，不知省市公仆们光临法院指导工作时，是否也会如此高举双手通过安检。<br />
&nbsp; &nbsp; 根据以往经验，旁听案件首先得领取旁听证，进入院内后随即四处打听何处发放旁听证，可问遍法院内所有过往人员，均称不知此事。当我和R君沿宽大的台阶拾级而上进入大厅时，发现厅内已聚集有三四百人，稍一打听，原来全是来旁听的，而且同我们一样，一个也没领到旁听证 ，好几位告诉我们，法院根本就没发放。<br />
&nbsp; &nbsp; 我一听当即明白要想进入庭内旁听已不可能，望着大厅内黑压压人群，说不定今天这庭外倒很可能会有一番热闹可看，好些年没见这种场面了，我们决意在此等待机会一饱眼福。<br />
&nbsp; &nbsp; 稍后大厅最里面忽然传来一阵大声喧嚷，我和R君赶忙穿过人群前去察看动静。近前一看，右边第五法庭门前正有一大群人围住两位法院工作人员理论，高声质问法院为什么不发旁听证？其中一个子女士声音特别尖利：“既然是公开审理，我们都有权旁听，你们故意不发旁听证，就是非法剥夺我们的旁听权利！”两位法院工作人员态度倒是很好，不论怎样质问，答复口吻非常平和：“不是不发，而是全部发完了，请大家理解。”正说话间，忽见有三位旁若无人般穿过警卫走到法庭门口值班桌前，然后各摸出一张纸片朝桌上一丢扬长而入。这时人群中有人叫了起来：“格老子！他们的旁听证哪来的？我们咋个领不到？”话音刚落，立即引起一阵哄笑：“这几个哥子都是群众演员，事先早就安排好的！”<br />
&nbsp; &nbsp; 值得一提的是挡在法庭前面的一溜排警察，个个身材魁梧，面容冷峻，无论人群怎么喧闹，始终双手后背、挺直身子站在比我们高两级的台阶上，只有个别人试图上台阶时，才伸出右臂优雅地做出一个谢绝近前姿式。这使我一下想起了当年“一打三反”运动时的公安兵，那般大爷若是放到今天，恐怕这会儿成都所有大小医院的救护车都拉着警笛往这儿狂奔了。<br />
&nbsp; &nbsp; 时间快到十点了，大厅内人群越聚越多，好些明显是农民模样的人也混了进来，照理说这些人是不会对旁听感兴趣的，样子又不象是被告的亲友，难道今天的案子会同他们有关？如果真是这样，这位能把贫下中农惊动的被告，看来还真不是一般角色。这时我特别注意到一个比我小不了几岁的老头，面容清瘦，满头白发，精神却特好，每挤到哪总有人围住他，这会儿正向身边人介绍自己：“老子是成都中级法院的常客，哪儿有官司，哪儿就有我，今天老子就是要看看这场好戏啷个收场？”那口韵味十足的川音，使我立即想起了李伯清的成都评书。<br />
&nbsp; &nbsp; 大概是人多得有些不象话了，这时有几位头头模样的人在高声发话，要大家别围在五号法庭周围，也别聚在厅内喧哗，法院特地拨出了大厅左侧的一号法庭供大家入内休息。原来站在法庭外台阶上的警察也全部下来，对通往五号庭的通道上实行了封闭。<br />
&nbsp; &nbsp; 拥挤中忽然发现人群里有个瘦高个正举着个相机在拍照。我一看不由暗自纳闷，这种场合拍照无疑是最为犯忌之事，怪的是怎么没人过问？。有几位一看可以自由拍照，立即掏出手机跟着拍了起来，谁知刚按两下快门，每个拍照者身边一下有人上前一把夺去了手机。我旁边有人立刻笑了起来：“真是大脑简单！咋个可能让你拍照？先前那照相的家伙是钓鱼的！”<br />
&nbsp; &nbsp; 站了半天，我和R君腿都酸了，于是去一号庭找个座位坐了下来。中国人爱热闹有传统，不知是谁居然把几个小孩带了进来，其中一个十一二岁顽童在原告、被告、书记员席位上折腾一番后，最后直登顶层一屁股坐在了中央的审判长高背椅上。这小子也不知是从电视上还是从哪学来的，忽然抓起审判席桌面的法槌缜昧似鹄矗口中还连声高呼“开庭！开庭！”一看如此有趣，下面好几人高声笑骂起来：“开你妈的槌子庭哟！”，“开你妈的槌子庭哟！”，全场顿时哄成一片。<br />
&nbsp; &nbsp; 稍坐不久，忽听大厅内传来一阵清脆的掌声，我和R君赶紧起身出门朝掌声响处挤去。只见两位中年男子正穿过人群向第五法庭门前走去，一边双手高举频频朝人群拱拳致意，从围绕他们的掌声看，这两位估计是北京来的律师了。其中一位领头模样者身形高大，气宇轩昂，不少人说这两位就是前不久轰动全国的邓玉娇案的被告律师，高个者即大名鼎鼎的X X X律师，究竟是否如此，不得而知。<br />
&nbsp; &nbsp; 老友R君在旁向我嘀咕：“怪事，成都有这么多律师，干嘛非得请北京的？”我一听不由笑了起来：“你想想，从今天这场面和架势来看，这案子居然有如此之多人来旁听，可见这被告肯定是个有点来头的家伙，当局关心他恐怕也不是一天两天了，对这样一个烫山芋，请问成都哪一家律师事务所敢接？今后他们还要不要在成都司法局手下混了？”R君听完我解释感慨地点了点头。<br />
&nbsp; &nbsp; 根据以往经验，刑事案开庭前律师一到，紧接就该带被告入庭了，我和R君紧盯着警察把守的一个空缺，我们倒要看看这位“政治犯”究竟是个什么样的红眉毛绿眼睛！可左等右盼好久却始终没再见有人进过第五法庭的门，自从律师进去后，那门早就关上。<br />
&nbsp; &nbsp; 这可有点怪了，被告不出场，这庭怎么开？莫非是缺席审判？人群中不少人也为此纷纷议论起来。有两位自称熟悉内情的男子说人早带进去了，庭也早开了，人是不可能从这里经过的，每个法庭内都有个后门。我和R君听了有些将信将疑，坊间百姓常常私下议论法院开后门，难道真的有后门？可是细想一下恐怕还真是这样，现在已经十点多了，再不开庭的话，那法官大人中午M不要饿肚子？<br />
&nbsp; &nbsp; 站了半天我和R君实在有些吃不消，再想去一庭找位子歇歇时发现早已人满为患，于是只好向厅外走去，看能否找个地方坐坐。走到大厅出口附近，看见有位女士正向周围人群大声诉说，说今天好多人不让来旁听，有的天没亮就有人把住家门不让外出，连上莱场买菜都不准，有的刚刚起床，立即被生拉硬拽请去喝早茶了。这位女士又说，还有些特地从外地赶来旁听的，一下火车就被盯上，结果都被堵在旅馆不让外出。说这些时她报了不少名字，我只记得其中有个叫“爱妹妹”的，刚住进宾馆当晚就被人狠狠修理了一顿，落了个鼻青眼肿。（ 从“爱妹妹”这名字看，想来此人似属猎艳族一列，大概是轻信传言误以为今天审理的是椿桃色案件，指望能亲眼一睹当事人芳容为快，谁知不明不白赶了趟浑水，结果白白吃了一番苦头 ）说话的这女子身材纤细，面容姣好，一副文静模样，如在街头遇见，肯定以为是位相夫教子贤淑女性，可谈吐之言辞锋利，尤其是一双纤秀美目所透出的寒光，足以令人感到切不可以貌取人。常云“自古蜀中多豪杰”，不想女流辈中也有如此强人。<br />
&nbsp; &nbsp; 出门在厅外宽大的石阶上坐了大约二十来分钟，看见从左边安检门进来一位三十来岁中年男子，小平头，墨镜，肩上背个黑色电脑包，边走边高嚷着什么，几位警察在其身后四五步处正不紧不慢跟着他，行至大门里口一间小屋前，小平头在一高台阶上坐了下来。这时几位警察不知为什么一下朝他围了过去，接着只听小平头喊了起来：“你们干啥子？你们干啥子？这是法院，这是人民的法院！是国家给了我旁听的权利，你们凭啥子要剥夺我的权利？！”我一听不免有些奇怪―――包括我和R君在内的千把人不都在这“旁听”？可也没见有人来赶我们出去，唯独这家伙一进来就如此受重点照顾，看来肯定有些来头！<br />
&nbsp; &nbsp; 正想事间，只见一名警察上前一把勾住小平头颈子，其余几位扭住平头身子连抱带推朝大厅右侧方向移去，不知是失去平衡或是故意赖地，平头一下四脚朝天仰八叉倒在地上，害得勾他颈子的那位随之也跌在了他身上。这时警察架起平头两只胳膊直接拖了起来，平头一面挣扎一面拚命在喊着什么，大门外街上好多人透过铁栏干向里喊“打人！打人！”。和我们同坐在台阶上的一位五十多岁戴帽子女士，看来也是位不简单人物，一下站起身尖声叫了起来：“大家快看，警察打人！警察打人！”话音未落，几个便衣立即围住了她：“哪个打人的？你看到谁打人的？”<br />
&nbsp; &nbsp; 说实话，这件事从开始到现在一直在我眼皮下发生，天地良心，警察确确实实没打平头，只是把他强行拖走而已，这位女士显然是夸大其辞了，于是我起身上前讲了句公道话：“这位女士您讲话得实事求是，我看的清清楚楚，那不是打，是拖――”最后一个拖字，我特地模仿四川话拖了一个长长的尾韵。<br />
&nbsp; &nbsp; 我原以为我的主动作证也许能博得便衣警察同志两句表扬，谁知话刚落音，立即有两张气势汹汹的脸一下对准了我：“你说啥子？你说啥子？你说啥子？”三个“啥子”音调一个比一个高，一下弄的我莫明其妙！<br />
&nbsp; &nbsp; 老友R君一见此景赶忙过来一把将我拉开，一直到二十米开外的安全距离后开始埋怨我：“老方啊，不是我说你，你刚才那话确实有些不讨人喜啊，难怪人家要冲着你发作。”<br />
&nbsp; &nbsp; 我说我没讲什么啊！我只不过是好心纠正那戴帽子女的用词不当啊！<br />
&nbsp; &nbsp; “亏你平时还摆弄文字。”老友鼻子轻轻哼了一声。“从通常字面意义看，这‘打’字似乎要比‘拖’字严重一些，可这两个字在司法衙门里自古以来却是另有一番讲究的，你可千万别以为‘拖’就比‘打’来的文明。”<br />
见我一脸疑惑，R君笑了笑继续给我上起文字课来。<br />
&nbsp; &nbsp; “不妨举例说明吧。比方古时某人犯事，青天大老爷惊堂木一拍：‘替我重打三十大板’，这是打；‘替我掌嘴二十’，这也是打。打的最严重后果无非是血肉模糊，鲜血淋漓，一般而言，对性命却是无大碍的。而这拖就不一样了：‘拖出去砍了！’，‘拖出去毙了！’，甚至还有拖出去喂狗、将妇女拖出去让弟兄们享受的事。可见凡是用到‘拖’字之处，那被拖之人的下场是绝对不妙的，而被打的往往只不过受点皮肉痛苦而已。你想，你这拖字能说吗？<br />
&nbsp; &nbsp; “另外，在中国这‘打’字有时还是一种关心、一种教育的特殊方式，中国百姓教育孩子素有‘不打不成器’之说，你可见谁说过‘不拖不成器’的？”<br />
&nbsp; &nbsp; 见老友越扯越有点离谱，我正想问他那把人打成右派、打成反革命，那个‘打’字该如何解释时，忽听大厅内又响起掌声，我和R君立即冲上台阶向厅内跑去。<br />
&nbsp; &nbsp; 刚进门，只见两位律师正在人群簇拥下往厅外走，好多人举着手机在拍他们，这时却没人管了。和两个多小时前进去不同的是，周围掌声虽然仍旧非常热烈，可这次律师没再向众人举手致意，那脸上分明是一副戚然表情。按我的经验，得胜的律师脸上总会有意无意露出些得意的笑，甚致会以V形手势来表达内心的高兴，从眼前两位律师的脸色看，这场官司大概是输了，这倒同我和R君的估计完全一致。本来嘛，在中国凡是和政治沾上边的案子，岂容被告胜诉？除非太阳从西边出！<br />
&nbsp; &nbsp; 这律师一出来，不用说庭审早已结束，被告自然照旧从后门被带走了，再待下去已没什么可看，抬腕看表已快到一点，遂同R君走出法院大门。同进门相反的是，出口处把门的警员对出门者非常客气，人在三米外便拉开铁门恭候，我们后脚刚刚跨出，身后铁门随即咣当一声锁上。<br />
&nbsp; &nbsp; 原指望来旁听的，目的虽然未达，也算旁观了整整一上午热闹，最遗憾的是，今天这位“政治犯”究竟姓甚名谁？到底为了何事惹上官司？庭审结果又是如何？到现在我和老友R君始终一无所知。<br />
&nbsp; &nbsp; 回到法官接待中心二楼吃了午餐，两人均有倦意，正打算午睡一会，忽有R君姐夫S先生来访。S先生是五十年代北京体院高才生，57年因“向党交心”交过了头，结果弄了顶右派分子帽子。我们这三个“过来人”一聚，很快就忘乎所以地共话起当年那些事。正说话间，房门突然缦炝似鹄础<br />
&nbsp; &nbsp; 刚拉开房门，两男一女三位警察走了进来，两位男的高大魁梧，女的长得挺秀气。通过门道时，其中一位还特地向空荡荡的卫生间内瞟了一眼。<br />
&nbsp; &nbsp; “我们是查房的，请予配合。”<br />
&nbsp; &nbsp; 查房？这二三十年来我不知住过多少次宾馆了，可这中午时间来查房倒是第一次碰到！<br />
&nbsp; &nbsp; 我不紧不慢把身份证递了过去，接身份那位在相片和我面孔轮流扫了十几眼后把证还给了我，接着又将目光移向了R君和S先生。我赶忙解释是成都的两位老朋友。就在查验我身份证的当口，其余两位仔细检查了房间每个角落，他们似乎特别注意我放在床边的那只摄影包，大约是没带搜查证，没好意思让我打开。<br />
&nbsp; &nbsp; 查了十分钟左右一无所获，三位不得不悻悻走人。老友R君特地礼送三位出门，关门时又特意客套了一句“欢迎再来”。<br />
&nbsp; &nbsp; 我们三人重新坐下五分钟不到，忽然又听有人砰砰敲门，进来的仍旧是方才三位。这次其中一位手中捧了一个大本子，一边看一边问：一个叫吴X X的住没住过这里？今天来过没有？<br />
&nbsp; &nbsp; 我说我从不知此人是谁，你们大概搞错房间了。三位盯住我看了会复又转身离开，这次走后再没人来。<br />
&nbsp; &nbsp; 警察的接连两次光临引起了我和R君的警觉。大白天中午一连两次查房，这决不会是普通的治安检查，他们一定是有目的而来，再准确点说，他们肯定嗅到了什么。我脑筋一转，立马想到了上午我们去法院旁听的事。大概是我们在法院大厅中待的时间过久，从而引起了有关人士注意，特别是我又多了两句嘴，一下被人盯上了。好在我和R君同那位“政治犯”八杆子打不到边，今天只是去看热闹，没想到的是看热闹也有可能会惹来麻烦，当今社会与时俱进的步伐确实越来越快了。<br />
&nbsp; &nbsp; 按老友R君夫妇的安排，我还得在成都再玩几天，可一想到自已有可能被人盯上了，觉得还是三十六着走为上。当前跨省追捕都是常事，到时折腾我这送上门的岂不更省事？我决定下午就去买明天回南京的机票。<br />
&nbsp; &nbsp; 次日傍晚我飞回了南京。作为游记，写了这篇小文。</div>

							
							
							
							
