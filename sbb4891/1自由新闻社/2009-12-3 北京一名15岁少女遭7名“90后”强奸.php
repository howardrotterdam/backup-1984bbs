<br />
&nbsp; &nbsp;&nbsp; &nbsp;本报讯 （记者刘杰）花季少女美美（化名）与家长赌气，离家出走进了网吧，被7名“90后”少年盯住，三次遭轮奸。记者昨天获悉，北京市朝阳法院以强奸罪判处7名少年有期徒刑10个月至11年不等。<br />
<br />
&nbsp; &nbsp; 美美现年15岁，今年2月寒假期间，她和母亲闹别扭，赌气离家出走。她来到小红门附近一家无照网吧上网，认识了网友王某等4名少年，便吐露心声，说自己跟家人吵架不想回家，但没地方可去。王某把美美带到朋友吕某开的一家黑网吧。“我跟他们一起玩，还上网，开始什么事都没有。”美美对警方说。<br />
<br />
&nbsp; &nbsp; 美美不知道，王某等人已经动了邪念，在一家旅馆开了房间。2月9日中午，王某等6人把美美带到旅馆，想和她发生关系，美美拒绝不从。王某在刘某、董某等人的帮助下，将美美强奸。<br />
<br />
&nbsp; &nbsp; 事后，王某等人一直看着她。过了几天，美美以上厕所为名想逃跑。郝某等人追上来，对她一阵拳打脚踢，“一个小男孩还用刀划伤了我的后背。”美美被拖进吕某住的房间，遭郝某、吕某、尤某强奸。遭受如此伤害后，美美逃跑无路，只得继续留在吕某的网吧里，每天上网解闷。2月22日晚上，正在上网的美美被吕某叫进房间。刚一进屋，门就被反锁。吕某再次强迫美美发生关系，刘某和董某也强奸了她。<br />
<br />
&nbsp; &nbsp; 仅仅半个月，美美连续三次遭到轮奸。这7名少年，最大的王某和董某19岁，最小的刘某案发时才14岁，其中李某、尤某均为在校学生。<br />
<br />
&nbsp; &nbsp; 今年3月，一名女士报警称在小红门附近被抢劫，还向警方反映有个女孩在网吧被强奸。警方当即赶往吕某开的网吧解救了美美，并将吕某和董某抓获归案。王某等4人随后到公安机关投案自首，在刘某的协助下，警方最后将郝某抓获。<br />
<br />
&nbsp; &nbsp; 法庭审理后认定，吕某等7人的行为构成了强奸罪，其中刘某、董某、吕某、尤某还对美美实施了轮奸。考虑到他们的犯罪情节程度不同，4人有自首行为，2人未成年，法庭酌情从轻量刑，判处他们有期徒刑10个月至11年不等，其中董某获刑最重，为11年。</div>

							
							
							
							
