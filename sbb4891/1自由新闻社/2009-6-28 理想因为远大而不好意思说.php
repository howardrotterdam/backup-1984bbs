													<h2>理想因为远大而不好意思说</h2>
						
						
																											<div id="postmessage_140002" class="t_msgfont">案发现场：<br />
<a href="http://gcontent.nddaily.com/e/70/e7023ba77a45f7e8/Blog/c0e/952474.html" target="_blank">http://gcontent.nddaily.com/e/70 ... log/c0e/952474.html</a><br />
<br />
配图：<br />
<img src="http://pics.nddaily.com/c/d8/cd89fef7ffdd490d/Thumb/7744/dkga062805.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
陈俊任<br />
&nbsp; &nbsp; 姓名：陈俊任<br />
<br />
&nbsp; &nbsp; 毕业学校：珠海一中<br />
<br />
&nbsp; &nbsp; 志愿：北大、清华或港大<br />
<br />
&nbsp; &nbsp; 选考科目：历史140分<br />
<br />
&nbsp; &nbsp; 总分：701分<br />
<br />
&nbsp; &nbsp; 第一次面对六七家省市媒体的镜头，陈俊任开始还略显局促。不过两三分钟后，她就调整得十分从容，确有一种“大将之风”。班主任马树林用了十六个字来概括这个逢考必第一的学生：积极向上，乐观大气，勤奋朴实，志存高远。马老师说，虽然陈俊任是个女生，但由于她的成绩和为人，所有同学都叫她“老大”。<br />
<br />
&nbsp; &nbsp; 从小到大考试就第一<br />
<br />
&nbsp; &nbsp; “不谦虚地说，从小到大的考试，全都是第一。”说是不谦虚，但陈俊任说话时仍略带羞涩。<br />
<br />
&nbsp; &nbsp; 高三刚开始的时候，陈俊任提出自己的高考目标就是――― 考到690分以上，争取状元。所以她今年以选考历史当上全省文科总分状元，在珠海一中人看来并不意外。连学校门卫都说：“那个女生我知道，她是成绩最好的。”<br />
<br />
&nbsp; &nbsp; 为什么她的成绩这么好？马树林说：“她就是为学习而生的。她并不是特别聪明的学生，她是聪明加勤奋。”<br />
<br />
&nbsp; &nbsp; 有一次开班会，有男生公然对陈俊任“老大”的称号不服，当着陈俊任的面对全班同学说“她能当老大，我也能当老大”。轮到陈俊任发言，她说：“你要是能超过我，我这十几年的老大就白做了！”<br />
<br />
&nbsp; &nbsp; 马树林在考前给她发信息：“沉着冷静淡定从容”，收到的回信是：“舍我其谁！”<br />
<br />
&nbsp; &nbsp; 精力旺盛就是爱跑步<br />
<br />
&nbsp; &nbsp; 马老师说：“一般来说，尖子生的人际关系可能没有那么好，但是陈俊任的人际关系却很好，而且集体荣誉感也很强。”同班同学陈清烨也说：“她很勤奋，而且很谦虚。我们问她问题，她总是毫无保留。她遇到不懂的问题，也会虚心地请教别的同学。”<br />
<br />
&nbsp; &nbsp; 陈俊任还是个“体育健将”。每次校运会，她都是班级主力，“所有能报的项目都报满，所有跑步的项目都是金牌。”<br />
<br />
&nbsp; &nbsp; “喜欢跑步的原因，套用朱自清的一句话，在这个时候你‘什么都可以想，什么都可以不想’。”<br />
<br />
&nbsp; &nbsp; 老师和同学还跟记者透露，陈俊任成绩好的原因还因为她的精力特别旺盛，每天早上5时起来看书，6时出去跑步。马老师也说，无论什么时候看见陈俊任，她都是健步如飞的，节奏特别快。<br />
<br />
&nbsp; &nbsp; 陈俊任说，到了高三，老师告诉她节奏应该慢一些，因此，对陈俊任来说，高三反而是一段较为轻松的时光。<br />
<br />
&nbsp; &nbsp; 志向远大不好意思说<br />
<br />
&nbsp; &nbsp; 老师们对陈俊任的评价还有一条就是志向远大，马老师用了“特别远大”。当记者跟陈俊任求证时，她仍有点不好意思，<strong>“我的理想不好意思说出来，太伟大了，说出来别人也不信。”<br />
<br />
&nbsp; &nbsp; 记者再追问，她说：“就是为共产主义而奋斗啊！” </strong><br />
<br />
&nbsp; &nbsp; 陈俊任从小生长在珠海西区的平沙镇，这让她有更多的机会接触到社会底层的农民、农民工和打工者。她的高考作文写的是关注农民工生存现状的叙事散文。“现在很多人的常识是农民都很脏而且很低下，但是人们都遗忘了一个真正的常识―――农民才是我们最亲的兄弟。”<br />
<br />
