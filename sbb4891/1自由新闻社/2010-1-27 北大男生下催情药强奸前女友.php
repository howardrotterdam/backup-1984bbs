&nbsp; &nbsp; 尽管肖琴不太情愿，但还是勉强把那杯味道有点怪的水喝了。至此，肖琴已经完全掉入陈某设下的陷阱，她刚刚喝过的那杯水中已经掺入催情药。 <br />
