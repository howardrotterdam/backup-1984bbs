<br />
&nbsp; &nbsp; 就在该案即将开庭时，肖琴突然给公检法三部门分别发信，表示她与陈某在去年5月3日发生关系是自愿的。警方立即开始调查并多次找肖琴谈话。最终，肖琴承认，她是受陈母多次恳求后，一时心软才同意递交一份翻供书面材料的。<br />
