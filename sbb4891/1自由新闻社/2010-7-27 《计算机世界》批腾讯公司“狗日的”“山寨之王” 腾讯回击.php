													<h2>《计算机世界》批腾讯公司“狗日的”“山寨之王”  腾讯回击</h2>
						
						
																											<div id="postmessage_761004" class="t_msgfont">“有什么业务是腾讯不做的吗？”美团网CEO王兴的语气中难掩郁闷。<br />
<br />
　　7月9日，腾讯QQ团购网上线，这让王兴如闻惊雷，也如坐针毡。从2003年回国到现在，王兴先后创办了校内、海内、饭否和美团4个网站，而美团网被他视为“最靠谱”的一次创业。3月初上线的美团网是国内第一家团购网站，创立仅仅4个月，美团网已经能够盈亏平衡。<br />
<br />
　　就在这时候，一直悄无声息的腾讯杀了进来，这让王兴完全猝不及防，也让处于草创时期的数百家团购网站倒吸了一口凉气。<br />
<br />
　　谁也不知道，这一次，这个“企鹅仔”将是搅局者、掠食者，还是终结者。<br />
<br />
　　别让腾讯盯上<br />
<br />
　　其实，王兴应该早就想到会有这么一天。因为在中国互联网发展历史上，腾讯几乎没有缺席过任何一场互联网盛宴。它总是在一开始就亦步亦趋地跟随、然后细致地模仿，然后决绝地超越。比如当初的游戏。<br />
<br />
　　“从QQ游戏平台上线那天起，联众的失败就已经注定了。”多年以后，在北京知春路的一家咖啡馆，联众创始人鲍岳桥谈起当年腾讯对联众的围剿和逼迫，仍然耿耿于怀。在两个小时的采访中，他连续抽了两包烟。<br />
<br />
　　联众是中国最早做游戏平台的公司，一度占有在线棋牌游戏市场85%以上的市场份额，在新浪、搜狐等门户网站亏损缠身的时候，联众是最早实现赢利的中国互联网企业，一时风光无两。<br />
<br />
　　2003年8月，腾讯QQ游戏第一个公开测试版本正式发布。鲍岳桥发现，从平台到游戏设计，QQ游戏完全是联众游戏的翻版。愤怒之余，“感到危险很大”的鲍岳桥首先想到的是“主动低头”寻求合作，于是他赶赴深圳，约见马化腾和时任腾讯公司首席运营官的曾李青，但是遭到了腾讯方面的拒绝。<br />
<br />
　　“现在想来，那时候是太天真了。”鲍岳桥说，“与大型网游不同，棋牌类游戏规则固定，没有技术门槛，玩家又与QQ用户高度重合，腾讯很容易模仿。”<br />
<br />
　　2004年9月，QQ游戏平台将联众赶下了中国第一休闲游戏门户的宝座。而在此之后，联众的业绩一路下滑，出售、转型，经历了一系列风波后，联众在中国网络游戏市场份额已不足1%。<br />
<br />
　　腾讯则扶摇直上，在今年一季度，QQ游戏同时在线人数达到了680万。而更重要的是，依托QQ游戏平台，腾讯终于在2009年第二季度超越盛大，坐上了中国网络游戏领域的头把交椅。<br />
<br />
　　对鲍岳桥来说，腾讯就是自己的终结者。2006年底，鲍岳桥离开了江河日下的联众，成为了一名天使投资人。他告诉记者，现在他做投资的原则之一就是：只做腾讯不会做、不能做的项目。所以三年来，他绝对不碰游戏，已经投资的医疗器械和数据存储项目都跟腾讯毫无关联。<br />
<br />
　　而这个终结者又有了新的目标，那就是“站长之王”蔡文胜的4399小游戏平台。<br />
<br />
　　“说不担心QQ竞争那是骗人的。”蔡文胜在微博上表达了自己的忧虑，直接原因就是今年7月初，腾讯旗下小游戏平台3366.com上线公测。<br />
<br />
　　据记者调查，去年蔡文胜买下的4399小游戏平台，通过广告联盟和联合运营网页游戏，月营收已达3000~5000万元，正在筹备国内A股上市。而腾讯刚刚上线的3366，在游戏种类和网站设计上与4399几无二致。<br />
<br />
　　而且这只“企鹅仔”似乎更加来势汹汹。从7月1日开始，不断有网游看到QQ弹窗对这一游戏平台的推广信息，而截止记者发稿时，3366.com同时在线人数已突破10万。<br />
<br />
　　只要是一个领域前景看好，腾讯就肯定会伺机充当掠食者。除了王兴和蔡文胜，腾讯最近还“默默地”动了另外一个人的奶酪，他就是奇虎360董事长周鸿t。<br />
<br />
　　5月31日，杀毒领域两大巨头360与金山的一场口水战激战正酣，腾讯的QQ医生3.3升级版却悄然上线。很快人们就发现，这款原本只是用来查杀QQ盗号木马的防护软件，已经了包含云查杀木马、系统漏洞修补、实时防护、清理插件等多项安全防护功能，甚至还搭载了免费半年的诺顿杀毒。<br />
<br />
　　此前，周鸿t曾在多个公开场合对腾讯创始人马化腾在产品上的功力赞不绝口，同时还声称，腾讯绝不会成为360的竞争对手，因为“腾讯是一个娱乐公司，在安全方面，应该由一个很专业的公司更专注地去解决问题”。<br />
<br />
　　很显然，马化腾毫不客气地给了周鸿t当头一棒。<br />
<br />
　　在腾讯还没有出手的互联网领域，小企鹅那些潜在的竞争对手们仍是战战兢兢，如履薄冰。比如暴风影音CEO冯鑫。自从2008年9月腾讯发布了本地播放软件QQ影音首个Beta版本，冯鑫恐怕就没睡过一天好觉。因为这款无广告、无插件播放软件，让暴风影音的盈利模式变得岌岌可危。<br />
<br />
　　而在各大视频网站因为版权打得不可开交，频频对簿公堂之时，同样有一种声音在业内流传：无论你们现在打得多欢实，等市场培育得差不多了，就该轮到腾讯来收场了。事实确实如此，QQLive的平台早就搭好了，拼版权，中国的互联网公司谁敢说自己比腾讯更有钱？<br />
<br />
　　这就是腾讯，中国第一、全球第三大互联网公司，一家全球罕见的互联网全业务公司，即时通讯、门户、游戏、电子商务、搜索等等无所不做。它总是默默地布局、悄无声息地出现在你的背后；它总是在最恰当的时候出来搅局，让同业者心神不定。而一旦时机成熟，它就会毫不留情地划走自己的那块蛋糕，有时它甚至会成为终结者，霸占整个市场。<br />
<br />
　　“某网站贪得无厌，没有它不染指的领域，没有它不想做的产品，这样下去物极必反，与全网为敌，必将死无葬身之地。”6月29日，新浪网总编陈彤以“老沉”为名发布了一则微博，言辞之激烈，让人震惊。这条微博迅速被转发了500多次，无数的人力挺“老沉”。<br />
<br />
　　谈起此事，一位互联网创业者几乎是脱口而出，“狗日的腾讯！”<br />
<br />
　　始终“贪得无厌”<br />
<br />
“既没有马云那么好的口才，也没有李彦宏那么帅。”马化腾曾经多次自嘲，说自己“很不幸”，“大家都是圈地，他们(马云、李彦宏)圈的都是楼，可以直接住。我们圈到的却是荒地，只能从铲沙、挖土开始，建自己的楼。”<br />
<br />
　　实际上，马化腾算不上纯粹的“草根创业”。据传，在腾讯创立初期，其父马陈术曾开着奔驰前来给儿子做账。在11年的发展历史上，腾讯只是在早期遭遇过资金困局，从获得第一笔融资开始就一直是稳扎稳打，先利用无线增值服务实现盈利，转而依靠互联网增值服务壮大，布局网络游戏和门户业务。2010年最新一季财报显示，腾讯的网络广告业务收入为2990万美元，已经远远超过网易的1340万美元，稳居门户第三。<br />
<br />
　　马化腾在业界以低调、务实著称，这在一定程度上决定了腾讯的企业风格：其疾如风，其徐如林，侵掠如火，不动如山。<br />
<br />
　　2006年7月，QQ同时在线突破2000万人，腾讯公司内部决定办一个庆功会，会上腾讯联席CTO熊明华问了马化腾一个问题：QQ同时在线人数何时能够到1亿？马化腾一笑：“这辈子我可能看不到了。”事实上，2010年3月5日，他就看到了。熊明华一定很后悔，没有和马化腾打赌“裸奔”。<br />
<br />
　　实际上，马化腾有很多值得“裸奔式”庆祝的理由。目前，腾讯是中国最赚钱的互联网公司，公司现金储备达到15亿美元；拥有中国本土用户量最大的即时通讯软件，账户数近10亿；是中国第一流量的门户；在网络游戏市场排名第一，占据超过20%以上的市场份额；电子邮箱流量也已经超过网易，雄踞榜首。<br />
<br />
　　资本市场对这只彪悍的企鹅也是极力追捧。在香港，腾讯的股价一度高达每股171.80港元，上市6年间腾讯股价上涨了超过了35倍。要知道，被世界公认为近年来最具创新能力的苹果，其股价增幅才只有腾讯的一半。<br />
<br />
是的， “腾讯不是一般的有钱”，但股东的钱不是用来供着的，腾讯必须不断寻找新的利润增长点。蔡文胜就曾表示，腾讯现在什么都想做，从中可以看出它面对快速增长的巨大压力，这个压力终有一天会压垮腾讯。<br />
<br />
　　在美团网创始人王兴看来，腾讯之所以染指团购，是因为这模式已经被证明“能赚钱”。“做团购没有技术门槛，盈利模式又清晰，腾讯没有理由不做。”王兴指出，团购与他之前创办的校内和饭否最大的不同在于，“网站从上线第一天开始就有收入”。――如此唾手可得的生意，腾讯怎么可能放过？<br />
<br />
　　搜索也将是腾讯的下一个目标。今年3月，马化腾与李彦宏在深圳有过一场对话。李彦宏问马化腾，“腾讯凭什么做搜索？”马化腾给出了两点理由：一是用户需要，腾讯这个一站式互联网服务平台中的很多环节都需要搜索功能；二是搜索能赚钱，腾讯拥有全球最大互联网社交网络系统，社区的盈利模式中，除了个人收费以外，未来还要结合页面内容分析，匹配相关性的广告。因此，已有业内人士指出，而在这一类似于Google的AdWords模式的探索过程中，腾讯未来必将对百度正在培育的广告联盟形成威胁。<br />
<br />
　　也许，在马化腾看来，无论是搜索，还是团购，甚至是将来的视频，这些业务都是腾讯水到渠成的业务延伸。因为马化腾为腾讯未来的构想是，一站式互联网服务提供商。――围绕腾讯QQ打造“在线生活社区”，也就是“用户要什么，腾讯就有什么”。百度董事局主席兼CEO李彦宏对腾讯所谓的“在线生活”、 “一站式服务”的评价是：基本上就是不给别人任何空间。<br />
<br />
　　在CSDN总裁蒋涛看来，腾讯之所以什么都做，是因为它是一家以人(用户)为中心的企业，同类型的企业还有软件巨头微软，两家公司的产品战略更是惊人的相似。<br />
<br />
　　长期以来，以操作系统为核心的微软也是个典型的“全民公敌”。为了“抓住”用户，微软每个阶段都会根据市场变化，布局新的应用，以巩固其用户终端的垄断地位。在个人消费领域上，微软先后推出了浏览器IE、邮件系统Hotmail、即时通讯MSN、邮件客户端outlook、免费杀毒软件MSE，以及今年5月刚刚发布的在线版Office软件。<br />
<br />
　　而从另一方面讲，腾讯的进攻也是一种防御。互联网产业往往形势突变，Google市值超越雅虎，Facebook流量超越Google都发生在旦夕之间。腾讯最怕的就是突然冒出一个企业，被一种意想不到的商业模式或竞争策略打败。所以腾讯对于任何一个互联网的新应用都不敢掉以轻心。<br />
<br />
　　“360安全卫士、暴风影音的装机量都已经上亿了，如果周鸿t或者冯鑫有一天跟新浪合作，也推新闻弹出框，马化腾不就郁闷了？”蒋涛认为，腾讯的产品策略之一就是：所有的互联网应用，只要用户量到了一定级别，腾讯一定要有，别人的产品可以暂时比腾讯做得好，但腾讯绝不会让它不可替代。<br />
<br />
　　当被问及腾讯的核心竞争力时，腾讯CTO熊明华给记者的答案不是超过10亿的QQ的注册用户，也不是某一项产品、技术方面优势，而是“耐心”：懂得在合适的时间推出合适的产品。”<br />
<br />
　　因此，究竟腾讯还会做什么，没有人知道。<br />
<br />
　　一直在模仿<br />
<br />
　　腾讯从来不做第一个吃螃蟹的人，却总能在成熟的市场中找到空间，横插一杠子。然而它选择的路径也使其饱受争议，那就是模仿，有时甚至是肆无忌惮地“山寨”。<br />
<br />
　　早在2006年，新浪网创始人王志东就公开指责马化腾是业内有名的“抄袭大王”，而且是明目张胆地抄袭。几年以来，类似的声音一直不绝于耳。直到最近，DCCI互联网数据中心主任胡延平还在质疑腾讯的创新能力，说它不仅不是卓越创新者，反倒是中小互联网企业的“创新天敌”。<br />
<br />
　　从模仿ICQ推出自己的第一款产品OICQ(腾讯QQ的前身)开始，腾讯似乎就埋下了自己的“模仿基因”――先是从韩国引入了QQ秀和其他一系列增值服务，又模仿新浪建起了门户网站；在网游领域，学联众开发平台，跟着盛大引进国外网游，随着网易自主研发，之后布局的C2C电子商务网站拍拍，以及第三方支付财付通，无一不是“山寨货”，这也是腾讯遭人恨的根本原因。<br />
<br />
　　“微博、杀毒、电子商务到今天的团购，这些领域的商业模式在那儿摆着，人人都在抄，你凭什么要求腾讯高抬贵手，不去挣这个钱了？”互联网资深人士谢文在接受记者采访时表示，业界这种对腾讯的埋怨，就像“小孩儿撒娇”，是五十步笑一百步。<br />
<br />
　　对于模仿的指责，马化腾的回应是：模仿是最稳妥的创新。<br />
<br />
　　“创新可以分为三个层次：技术创新、产品创新和应用创新，产品和应用层面的创新比较容易被人忽略。”一位资深互联网产品经理告诉记者，几乎腾讯的每款产品都能找出市场上其他同类产品所没有的优点，如腾讯QQ的群和显示最近联系人功能，QQ邮箱的超大附件功能，QQ游戏平台一上线就号称能承载上千万的同时在线，QQ还解决了困扰很多IM产品的联通、电信的互联互通问题等等。<br />
<br />
　　事实上，腾讯获得突破的领域往往得益于应用层面的创新，腾讯总是能够通过QQ用户行为习惯的把握，将新产品与腾讯QQ这一核心进行结合，使其用户的优势得到发挥。同为技术出身，奇虎360董事长周鸿t坦言如果同是做即时通讯，自己在产品细节和技术上能够比马化腾做得好，但很难比QQ成功。因为马化腾是把互联网产品当成服务来做，其成功在于“打动人心”。<br />
<br />
　　CSDN总裁蒋涛在接受本报记者采访时也表示，虽然从商业竞争的角度，腾讯通过复制别人的商业模式进行无限扩张，是无可厚非的，但在客观上必然会扼杀一些创新的好苗头。这也和胡延平的观点一致，从某种程度上说，腾讯是互联网创新者的杀手。<br />
<br />
　　腾讯的麻烦<br />
<br />
　　四面制造麻烦的腾讯并非每次都能凯旋而归，甚至给自己惹上了不少麻烦。2009年6月，搜狐就因为输入法将腾讯告上法庭，称腾讯侵犯了其旗下搜狗拼音输入法的软件自主知识产权，并且利用QQ拼音输入法破坏搜狗拼音输入法服务，对搜狗实施不正当竞争，因此请求法院判令腾讯停止不正当竞争行为，并索赔2000万元。<br />
<br />
　　能够让同为互联网巨头的搜狐撕下脸面，腾讯“与全网为敌”所招致的民愤可见一斑。<br />
<br />
　　不过，身为山寨之王的腾讯也在遭遇“被山寨”。2005年成立的51.com，几乎腾讯每推出一个新的功能与应用，它都会加以“学习”、“消化”，并迅速在自己的平台上开发出来。如目前在51.com平台上的“51商城”、“51群组”、“51秀”、“51问问”，它甚至曾经开发出彩虹QQ，免费提供IP地址探测、显示隐身好友等腾讯QQ的“增值”功能。<br />
<br />
　　“能不能给大家一点建议，怎样才能抗衡腾讯呢？”在2009年游戏产业年会的高峰对话环节，当主持人抛给腾讯游戏总裁任宇昕这样一个问题时，除了任宇昕自己一脸骄傲，举坐皆苦笑。这位中国最大互联网公司的游戏业务负责人也不谦虚： 只有跟腾讯合作，共同把市场一同做大。<br />
<br />
　　在外界看来，腾讯庞大的身躯，依然潜伏着诸多暗流。实际上，因为腾讯在互联网界“无耻模仿抄袭”的恶名，使得腾讯全线树敌，成为众矢之的。当越来越多的互联网企业开始时时提防着腾讯的时候，腾讯将不再像以前那样收放自如。比如，为应对腾讯的搜索，百度就将搜索的提成比例从10%提升到15%。<br />
<br />
　　而且，腾讯还算不上真正强大。互联网资深人士谢文则表示，腾讯的模仿充其量只能让保持强大的现状，却不能使其引领潮流，真正走向伟大。“事实上，如果腾讯一味模仿下去，随着平台上的服务越来越多，单个服务的效率会大幅降低。”谢文表示，“而且，如果腾讯只是针对现有的QQ用户群体开发应用，未来QQ用户的人口特性将被固定在年轻群体的娱乐需求上，随着网民年龄结构的变化，腾讯就会被最终边缘化，而开心网、人人网及新浪微博(<a href="http://t.sina.com.cn" target="_blank">http://t.sina.com.cn</a>)的崛起已经为腾讯的迟钝敲下了警钟。”<br />
<br />
　　在很多人眼中，腾讯是最近接近Google的一家本土互联网公司。因为虽然Google目前的主要盈利点还是围绕其搜索产品的AdSense和 AdWords，但它也是邮箱、地图、音乐无所不做，腾讯也是如此，虽然号称全民公敌，但它的主要收入仍然来自IM和网络游戏所带来的互联网增值服务。<br />
<br />
　　但谢文却指出这只是表面现象：“腾讯和Google完全不在一个档次上”。他指出，Gmail、Google地图、Google Earth等产品虽然不赚钱，但是它们之所以被开发都是围绕着一个核心理念：就信息整合与信息呈现。相比之下，腾讯的产品则显得杂乱无章，IM、网游、电子商务与门户业务之间并不必然的关联，其他公司单独做也能成功。据此，谢文认为，腾讯只是利用先发优势抓住了一大批用户，产品研发都是针对用户市场展开，追求短期效益，而对自己的未来缺乏清晰地规划。<br />
<br />
　　“建立在用户群上的腾讯是不牢靠的。”蒋涛认为，一旦未来人们更喜欢用Facebook和Twitter这样的工具彼此联络，不再以IM为中心，腾讯的“大本营”就被攻克了，这意味其虚拟货币系统必将被超越，而网游、门户这些现有盈利点也不能保证一直有市场竞争力。<br />
<br />
　　“如果人们未来都不再依赖PC，改用Ipad和手机的话，微软无疑就完蛋了”蒋涛说。微软的今天可能就是腾讯的明天，IT产业往往形势突变，用户习惯的变化又是在旦夕之间，看看facebook和google所带来的一场场变革，腾讯当以微软和雅虎为戒。<br />
