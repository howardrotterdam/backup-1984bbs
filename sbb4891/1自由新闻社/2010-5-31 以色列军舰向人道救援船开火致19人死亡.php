													<h2>以色列军舰向人道救援船开火致19人死亡</h2>
						
						
																											<div id="postmessage_649940" class="t_msgfont">新华网耶路撒冷5月31日电 来自以色列军方的消息说,以军目前已控制了国际救援加沙的人道主义船队的船只,正将它们拖向以色列南部港口阿什杜德。另据以媒体31日报道,以军舰拦截国际救援船队的行动已造成19人死亡。<br />
<br />
以军31日上午发表声明说,以色列海军当天黎明前采取行动,拦截6艘试图驶往加沙的船只,以军人员登船时遭到攻击,袭击者使用了枪支和其他器械,并抢夺以军枪支射击,以军开火还击。初步报告说,冲突中至少10人死亡,多人受伤,受伤者中包括4名以军士兵。<br />
<br />
以军目前已控制这些船只,正将它们拖向以色列南部港口阿什杜德。以军将在那里对船只、船上人员和货物展开全面检查。<br />
<br />
另据以色列电视台“第10频道”报道,开火事件已造成19名平民死亡,26人受伤,伤者中包括5名以军士兵。<br />
<br />
来自声援巴勒斯坦人、反对以色列封锁加沙地带的国际和平组织“自由加沙运动”的消息说,以军士兵当天4时30分左右趁着夜色从直升机上降落到国际救援船队中的“马维 马尔马拉”号上,一落到甲板上便对还在睡梦中的人们开火。<br />
<br />
“自由加沙运动”自2008年以来多次组织船只从海上突破以色列封锁进入加沙地带。该组织也是本次国际救援船队的组织方之一。<br />
<br />
<br />
由40多个国家资助的国际人道主义救援船队计划当地时间31日中午抵达加沙地带沿海地区。船队共装载了一万多吨医疗用品、建筑材料、帐篷、食品等物资,船上人员共750人,来自欧洲及阿拉伯国家等。这是自2007年以色列对加沙地带实施封锁以来,规模最大、人数最多的一次国际声援加沙行动。船队明确表示,无论此行多么危险,都将试图在加沙地带靠岸。<br />
<br />
以色列明确表示拒绝船队直接进入加沙地带,并派军队进行拦截。据以色列电台报道,31日凌晨,三艘以色列军舰靠近正在航行中的船队,警告他们加沙地带水域是军事禁区,任何企图靠近的人员都要后果自负。随后,以色列战斗机不断在船队上空盘旋,对其行程进行监视。<br />
<br />
<br />
■ 反应<br />
<br />
土耳其爆发万人反以游行<br />
<br />
土耳其召回驻以大使；国际社会谴责以色列 <br />
<br />
5月31日的冲突事件激怒了土耳其。土耳其召回驻以大使以示抗议。该国国内爆发了上万人的反以游行。<br />
<br />
从阿拉伯媒体播放的画面看到，以色列士兵正在开火，并登上了土耳其船，打伤了船长。电视画面的最后，有人用希伯来语大喊：“所有人都给我闭嘴！”<br />
<br />
土耳其副总理阿林奇5月31日宣布，召回土耳其驻以色列大使。正在以色列进行训练和比赛的土耳其青年足球队将立即回国。土方也取消了3个与以色列举行的联合军演。<br />
<br />
当天以色列驻伊斯坦布尔领馆遭到土耳其上万民众的包围。人们焚烧以色列旗帜，高呼“以色列是罪犯”等口号。人群试图冲进领事馆，但被警方制止。<br />
<br />
巴勒斯坦民族权力机构主席斯称此事为“大屠杀”。哈马斯表示，以色列的“罪行”终将得到报应。<br />
<br />
联合国秘书长潘基文也对该事件表示谴责。希腊、埃及、瑞典、西班牙、丹麦等国都召见了以色列大使，对此事表达关注和抗议。<br />
<br />
白宫发言人称：“美国对造成的伤亡表示遗憾，正在努力了解事情的来龙去脉。”<br />
<br />
■ 中方反应<br />
<br />
中国谴责以袭击救援船队<br />
<br />
本报讯 （记者邢世伟）昨日，外交部发言人马朝旭表示，中方对以色列海军攻击向加沙地区运送人道主义物资的土耳其船队并造成严重人员伤亡表示震惊和谴责。中方敦促以色列方面切实执行联合国安理会有关决议，改善加沙人道主义局势。<br />
