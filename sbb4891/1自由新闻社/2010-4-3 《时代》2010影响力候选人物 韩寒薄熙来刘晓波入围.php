大众网4月3日报道 美国《时代》周刊最新启动的一年一度的100位“全球最具影响力人物”评选，于北京时间今晨公布“全球最具影响力人物”的200名候选人名单，包括国务院副总理王岐山、重庆市委书记薄熙来、百度总裁李彦宏在内的多名中国人成为候选人。<br />
