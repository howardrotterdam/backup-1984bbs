<br />
大众网4月3日报道 美国《时代》周刊最新启动的一年一度的100位“全球最具影响力人物”评选，于北京时间今晨公布“全球最具影响力人物”的200名候选人名单，包括国务院副总理王岐山、重庆市委书记薄熙来、百度总裁李彦宏在内的多名中国人成为候选人。<br />
<br />
2010《时代》百人榜5月1日截止投票<br />
<br />
《时代》从2004年开始每年评选100位“全球最具影响力人物”，按照“领导人与革命家”、“创业者与企业家”、“科学家与思想家”、“英雄与偶像”以及“艺术家与娱乐界人士”五大项目，选出当年全球在各行各业引领风潮的百位最具影响力人物。<br />
<br />
在2010年的《时代》“全球最具影响力人物”候选人名单中，美国总统奥巴马、美国国务卿希拉里、日本首相鸠山由纪夫、新加坡前总理李光耀等政坛人物纷纷上榜。<br />
<br />
企业家方面，比尔・盖茨和夫人梅琳达不出所料地成为候选人，而近来受“召回门”困扰的丰田汽车总裁丰田章男的名字也出现在名单中。<br />
<br />
娱乐名人方面，美国著名脱口秀主持人奥普拉・温弗莉、“选秀之父”西蒙・考威尔等成为候选人；在温哥华冬奥上令人目眩的韩国花样滑冰名将金妍儿以及绯闻不断的美国高尔夫球手泰格・伍兹等体育界名人也都进入了候选名单。<br />
<br />
今年评选的在线投票已经开始，并将于5月1日截止，《时代》之后将从候选名单中选出100位“全球最具影响力人物”。<br />
<br />
中国上榜人物<br />
<br />
王岐山“带领”中国度过经济衰退<br />
<br />
《时代》周刊指出，国务院副总理王岐山作为中国最高层的经济官员之一，成功带领中国度过全球经济衰退，使中国变得比以往任何时候都要强大。他将会在今年5月代表中国参加新一轮中美战略经济对话。<br />
<br />
入主重庆薄熙来打黑深得民心<br />
<br />
《时代》在介绍重庆市委书记薄熙来时说，这位前中国商务部长自2007年担任重庆市委书记以来，大力打击重庆的黑帮有组织犯罪，这使他成为中国最受欢迎、最深得民心的官员之一。<br />
<br />
百度总裁也入选“最具影响力人物”<br />
<br />
百度总裁李彦宏也成为本年度《时代》“全球最具影响力人物”的候选人之一，这位41岁的年轻企业家于1999年成立了百度网络搜索公司，目前百度在中国搜索市场的占有率已接近60%。<br />
<br />
其他中国上榜者还包括青年作家韩寒，《时代》表示，这位27岁的年轻作家在出版以自己的中学辍学经历为背景的第一本小说后一炮而红，成为中国最畅销的作家之一。<br />
<br />
另外，台湾宏集团行政总裁王振堂的名字也出现在了候选名单中。《时代》介绍说，王振堂掌管着世界上发展速度最快的电脑公司之一，这家台湾企业已经成为目前全球第二大个人电脑生产商。 文/记者刘晓雪（法制晚报）</div>

							
							
							
							
