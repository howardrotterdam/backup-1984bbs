													<h2>作协大佬要求韩寒学好哲学</h2>
						
						
																											<div id="postmessage_84459" class="t_msgfont">铁凝：韩寒应该学点哲学<br />
<br />
一向不喜欢以教师的口气来表达自己的意见，但对韩寒新近发表针对成龙话语的言论，我觉得有必要破例，来讨厌一回。<br />
　　韩寒应该学点哲学，然后再写文章。　　为什么我觉得他这方面非常欠缺呢？　　韩寒说：对于政府和人民的关系，我们通常有两种认识，一种就是成龙说的“管理“，一种就是一小撮人说的“服务”，政府自然愿意做老板而不愿意做服务员，因为服务员只能按照行业惯例来收费，而老板可以自定义收费，服务员只能在法律规定的范围内行事，而老板可以制定法律。赞同需要管理和赞同需要服务有什么区别呢？区别就是你到底是能成龙还是成不法分子。（来自他博客《像成龙一样学会揣摩圣意》原文，打错的标点也是）<br />
　　如果他稍微懂一点哲学，是不会打着“我们”的旗号，把“管理”和“服务”简单对立起来的。　　这里需要引用一个最简单的“对立统一”概念，估计大家都知道这个概念的内涵。<br />
　　就一个国家而言，政府的管理与服务功能就是这样一种概念，管理其实也是一种服务，而服务同样也是一种管理方式。如果韩寒想不通，就想想你的家庭，爸爸不让你骑着童车到马路上跟公汽赛跑，是管你还是爱你？如果妈妈忘了给你喂牛奶，她是没有为你服务好，还是没有管理好你的生活？　　也许韩寒会说，政府不是我的爸爸妈妈，我只想让政府服务好我，不想让政府管理我。<br />
　　如果懂点哲学了，就不会这么幼稚了。道理很简单，要想服务好，就得管理好；要想管理好，服务上就更得加强。最一目了然的，要想让韩寒们“能成龙”，政府首先要把不法分子尽量关起来，否则不等韩寒们小学毕业，可能叫人绑走撕票了，法律不让不法分子这么做，但不法分子不是读了法律书自己就跑去坐牢的，是政府开动国家机器，用强制手段把不法分子抓进去的。　　这就是服务与管理的哲学关系。<br />
　　不懂哲学，甚至不懂最简单的概念，就急着“语不惊人死不休”，是很愚蠢的，也会对你的听众或读者，尤其是喜欢你的同学们产生非常不好的影响，说严重一些，就是用谬论害人。明明是对立统一，而韩寒却一昧地定义为对立，结果就是煽动一种民众与政府的对抗情绪，这是非常不负责任的，也是极其无知的。<br />
　　韩寒的博客，针对政府的斥责和批评非常多，许多朋友，特别是青少年朋友，觉得他是讲“民主”，但是我可以负责任地说，这是一种地道的伪民主，是一种真正的哗众取宠。真正的民主思想，绝对不是把政府当成万能的把子，出了什么问题都可以归疚过去。政府只是我们大家用来获得理想中的安全与幸福、解决某些不利于社会进步发展的问题，而采取的一种设施或者说是工具，好比植树时带的铁锹，有的同学，比如不懂哲学的韩寒，挖不好树坑，就一味怪铁锹不好使，懂哲学的同学，比如我，除了铁锹，还会想一想，这里边有没有自己的问题，比如力气使的不对，地下是不是藏了石头。<br />
　　懂一点哲学并不难，韩寒不会忙到没有时间学习，非常明显，他就是要靠骂铁锹证明自己的“大义凛然”。我想对他说的，首先学学哲学，其次不要再把过时的挑唆性手法，当成立身之本，何况那的确不是真正的民主精神。这方面大家应该了解一些较平实但是却很进步的理念，我很喜欢熊秉元教授的话，他只是希望民众获得更多资讯，知道自己的利益所在，而对于政府，他说：“我们好不容易才脱离权威式的政治结构，呼吁‘当局放手，民众自觉’犹恐不及，怎么能再企求当局‘展现魅力、大刀阔斧’。那不是意味着希望过去的绝对威权死而复生吗？那不是在民主政治发展上开倒车、走回头路吗？”熊秉元教授是台湾的经济学家，他的思想也许与大陆有所不同，但是在学者乃至民众怎样对待政府上，我是绝对同意他的观点，这段话我认为足以醒示韩寒。<br />
　　“曾经有一位历史学者说：一个社会的好坏就看这个社会能不能产生出伟大的领袖！工人哲学家贺佛尔有不同的领悟，他说：一个社会的好坏，就看这个社会在没有伟大领袖的情形下，是不是还能正常运作！也许，当我们能在不寄望当局大有魄力、一呼百应，而还能过得悠游自在时，我们的社会才可以算得上是一个已经上轨道的现代民主社会吧……”<br />
　　说了这么久，我只希望读惯了韩寒文章的同学们，能冷静地思考一下，我们是不管三七二十一，像韩寒那样，凡事都埋怨政府，以对立于政府为荣，还是脚踏实地，以自身的本领，帮助政府进步，真正造福社会，服务人民？是要在政府是管理我们的、还是服务我们的这样富于挑唆性的问题上无知地争吵不休，还是目光长远、胸怀大志，去为振兴国家和民族做一些真正有意义的事？　　我想大家自己会有正确的抉择。<br />
