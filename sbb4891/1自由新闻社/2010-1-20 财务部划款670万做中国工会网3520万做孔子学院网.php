　　在百度键入关键字“最贵网站”，当3520万的字样跳出来时，建峰暗吸一口凉气，平时过手的网站，基本上10万元就是一大关，“千万建站，难道要做个新浪、搜狐？”点开新闻仔细研读，发现只是一个政府网站，且3520万元不包括软硬件费用时，建峰马上给朋友留言，“跟我们不一样”，他顿了一下，微微一笑，紧接着打出了几个字：“他们这三千万卖的可不是技术”。<br />
<br />
　　“最贵网站”事件起源于财政部网站的一则中标公告，招标编号为TC099R72的“中国工会网扩建项目一期工程（网站改版、内容管理、站内检索、统计分析）”的公告引起了网友们的注意――看似简单的功能居然成交价670万元，绝对称得上是“天价标书”。1月20日上午，一位网友将这则公告贴在了微博里，称自己找到了“全球最贵网站”，其微博浏览量随即飙升。<br />
<br />
　　热心的网民纷纷跟进，发挥“群众力量”。很快“最贵网站”的记录便被打破，一则发布于2009年12月15日招标编号为 CEIECZB01-09JX033的标书浮出水面：这个“网络孔子学院网站运营服务项目”的采购人为国家汉办（孔子学院总部），第一包的中标金额高达 3520万元！同样，数千万的成交额只涉及“运营”等软性服务，并不包含硬件采购成本。<br />
<br />
　　对于这两起高标建网站的事情，网友Ice在cnBeta上跟帖留言，“怪不得最近科技股这么疯，做个网站都能赚好几百万”；记者MSN上一个朋 友更将签名改为“孔子说：３０００万元这么贵，干脆把我卖了吧！”嬉笑怒骂间，无奈之情溢于言表。<br />
<br />
　　除了“嘲讽派”，“写实派”也在网络舆论中扮演重要角色，网友“woshihaojie”留言，“我也是做网站的，这种网站超过10万就是讹诈！”更有网友以娱乐的形式搞起了“低价竞标”：1200元、1000元、800元、500元……“这种普通CMS（内容管理系统）就可以搞得定的站，华强北500块有的是人接。别说还不是建站，只是改版”。<br />
<br />
　　记者在中国政府采购网上找到另一则“中国工会网扩建项目一期”的招标公告，主要采购建站所涉及的软硬件设备，包括服务器（44台）、磁盘阵列（4部）、光纤交换机（2台）、邮件服务器（1台）等9类产品60余部高端设备，中标金额为715万元，供应商为广东亿迅科技有限公司。<br />
<br />
　　有网友将两组中标数据对比，感叹“科学技术是生产力”的伟大结论：动动脑，一两天的工夫随便给网站做个改版，就够买60台大型服务器了，“以后 谁还敢说网站程序员是IT民工？”<br />
<br />
　　面对越演越烈的争议，国家汉办综合处夏建辉主任终于在1月21日接受采访时表示，网友的解读不全面，此次招标并非单纯建设网站，中标公司还要负责网站的运营管理服务、网站发展规划、技术保障、软件开发、包括提供汉语教学音频、视频、课件等资源的制作、下载在内的一系列内容建设。“网站是要建设成一个汉语学习的门户网站，要面向全球推广使用，这是一个综合项目，是包括建立网站在内的一揽子工程。”<br />
<br />
　　夏建辉表示，此次招标是委托招标公司面向全社会公开招标，招标公司对五洲汉风按照国家相关规章制度进行了审查，其资质肯定没有问题：五洲汉风作 为国家汉办直属企业的身份也肯定经过了审查，没有问题。<br />
<br />
　　调查&gt;&gt;<br />
<br />
　　相关网站、公司集体装傻<br />
<br />
　　1月22日上午10点，记者经过1个小时不停拨打电话后，终于拨通了汉办宣传处的电话，想详细咨询一下网站改版花销的相关细节，但当知道是记者 的来意后，让人哭笑不得的一幕发生了。<br />
<br />
　　记者：是国家汉办吗？我是电脑报的记者，想就招标一事简单做个采访。<br />
<br />
　　汉办：你是谁？我听不清？<br />
<br />
　　记者：电脑报的记者……喂，喂？<br />
<br />
　　汉办：听不清楚，你等等再打来吧？信号不好！<br />
<br />
　　……挂断电话<br />
<br />
　　1月22日上午9点，记者拨通了中国工会网网站下方的联系电话，记者说明采访来意以后，对方负责人只说了句“不清楚”就直接挂断电话。<br />
<br />
　　而对于五洲汉风公司，记者无论如何也找寻不到它的联系电话，只在北京市工商行政管理局海淀分局的备案中看见，“五洲汉风”的注册地址为“北京市 海淀区学院路15号楼1区教2楼225”，正是北京语言大学的一栋教学楼里。<br />
<br />
　　中软宏大是什么来头<br />
<br />
　　事有蹊跷，中标供应商的身份成为人们茶余饭后的谈资，“网络侦探”们也纷纷介入，承接工会网扩建项目的“北京中软宏大信息技术有限公司”首先被推上舆论风口。在博客“幼学笔记”上，记录了博主对事件主角的“研究成果”：这家有能力做670万元“大项目”的高科技企业的网站，甚至连基本的火狐、傲游浏览器都无法兼容，只能用IE访问，“不然一打开就是一团乱码”，博主公开质疑该公司的信息技术实力。<br />
<br />
　　更为可笑的一幕继而出现，这家号称“专注于软件研发、系统集成与网络安全的高新技术企业”的官方网站，1月21日起竟无法访问，提示“找不到网 页”。<br />
<br />
　　“昨晚我还进了他们网站，并做了截图”，民间维权人士张洪峰1月20日晚接到网友爆料，对中软宏大进行跟踪，进入网站后，看到中华总工会与中软宏大的签约新闻。不过，与网站改版、内容管理、站内检索、统计分析的中标内容不同的是，双方合作的内容变成了VPN网络工程（VPN，Virtual Private Networ 虚拟专用网络），可以理解为虚拟化的企业内部专线）及信息报送系统，“奇怪的是，合作双方、时间、地点等要素完全符合，670万元的网站改版却只字未提，最后变成了VPN系统签约”。<br />
<br />
　　张洪峰为记者提供了Google关于“中软宏大”网站的缓存，可以粗略浏览到“中软宏大”网站的全貌。其界面与多数企业网站类似，但可以看出其内容较多，共包括17个板块。“应该是他们自己关闭的”，张洪峰直截了当地说，“如果真的是被攻击导致不能浏览，对于一家网络安全公司来说，那可真是他们的悲哀”。<br />
<br />
　　越来越多的蛛丝马迹慢慢被发掘出来：虽然采购方中国工会网络中心委托中招国际招标有限公司进行招标相关事宜，并在财政部网站公示中标结果，但遍寻网络，无论财政部官网的“招标公告”板块，还是采购信息专业发布平台“中国政府采购网”，根本找不到该项目的招标公告。只有2009年12月25日发布的“中标公告”。换言之，该项目极有可能没有公开招标，而是采取了其他方式。<br />
<br />
　　记者经过多方调查发现，中华总工会与中软宏大颇有渊源。中软宏大是中软集团下属公司，长期为中华总工会网站提供技术支持，包括网络维护与管理， 这正与此次招标主体工会网络中心的工作职能接近。<br />
<br />
　　不妨这样理解，中软宏大在一定程度上成为工会网络中心的“亲信”与“雇佣军”。由于这层特殊的关系，此次工会网扩建工程中，中软宏大收获颇丰。除了备受争议的670万元的天价订单外，记者还找到了另外一份编号为TC099Q77、1月4日公开的标书，1包的承接供应商正是中软宏大，金额为 28.6万元。奇怪的是，中标项目一栏没有任何详细内容。也就是说，总工会花了28.6万元，却没有人知道他们买了中软宏大什么产品。<br />
<br />
　　五洲汉风赶巧中标<br />
<br />
　　相比中公网的改版，网民更关注“网络孔子学院网站运营服务项目”。<br />
<br />
　　该项目供应商为“五洲汉风网络科技（北京）有限公司”，记者尝试在网络上搜寻这家公司的信息，却毫无收获――这家承接了3520万元项目的公司，不仅没有官方网站，甚至没有任何公开的公司信息，只找到了一家名称接近的“五洲汉风教育科技（北京）有限公司”。难道财政部网站上的公示标书出现了笔误？记者进入“北京市企业信用系统”进行查询，一切真相大白：这家“五洲汉风网络科技（北京）有限公司”成立于2009年9月9日，注册资金600万元，企业法人为王永利。<br />
<br />
　　因为成立时间太短，且基本没有开展业务，所以网络上信息寥寥，甚至连商务部的系统中都未来得及更新该企业的信息。就是这样一家刚刚成立的新公 司，承接了3520万元的巨额订单。<br />
<br />
　　更多的内容陆续被网友“人肉”出来，“五洲汉风”法人代表王永利的另一重身份，正是国家汉办副主任、孔子学院副总干事。由于这层关系，“五洲汉 风”拿到汉办订单似乎也在情理之中。<br />
<br />
　　另外，“五洲汉风”的成立时间也比较“赶巧”。汉办此次项目招标截止日期是2009年12月9日，2009年11月中旬对外公开招标，2009 年9月9日刚刚进入营业状态的“五洲汉风”，“刚好”赶上了这次招标。<br />
<br />
　　“北京市企业信用系统”中，在企业“经营范围”一栏，“五洲汉风”留下了这样一组令人匪夷所思的文字：“法律、行政法规、国务院决定禁止的，不得经营；法律、行政法规、国务院决定规定应经许可的，经审批机关批准并经工商行政管理机关注册后方可经营；法律、行政法规、国务院决定未规定许可的，自主选择经营项目开展活动”，如此才草草应付了事，让人怀疑“五洲汉风”是否真的有经营实业、开拓市场的决心？<br />
<br />
　　成本分析&gt;&gt;<br />
<br />
　　我给两个网站估估价<br />
<br />
　　3520万元可以用100年<br />
<br />
　　发言人：某省信息产业厅信息化负责人 王秘书长<br />
<br />
　　3520万元对于网站改版来说简直是天价，如果按我们省里的标准计算的话，这笔费用起码可以用100年。<br />
<br />
　　先举两个例子，我们省里信产厅的官网建站成本也不过30万元。而我们厅里下设的一个机构建站成本为15万元。现在想想，这个15万元还真是价格 太高了点。不过和中国工会网和网络孔子学院两个网站项目金额比起来，就完全不在一个比较线上了。<br />
<br />
　　常常有人会说，软件开发的投入和成本是无法计算的。我并不同意这个观点，比如我就看到一个软件行业普遍认同的软件开发估算办法，并且还有相关公 式：<br />
<br />
　　软件开发价格 ＝ 开发工作量 × 开发费用／人・月<br />
<br />
　　软件开发工作量 ＝ 估算工作量经验值 × 风险系数 × 复用系数<br />
<br />
　　这其中，风险系数分为5个等级，而复用系数是指已经建立能够复用的构件库（核心资产库）或者已有的软件产品，仅作二次开发，能够减少软件开发工 作量的工作。不过，这个公式里还包含着经验和一些其他的数据，比如维护费等等。<br />
<br />
　　建站标准不一价值难衡量<br />
<br />
　　发言人：摄影人之家网站站长贾富<br />
<br />
　　建站成本无非分硬件和软件两部分。除了硬件方面服务器、交换机等可控的成本以外，其他的软件和人力成本都是可大可小的，也是无法用统一尺度衡量 的。<br />
<br />
　　首先，网站策划费用可以是0元，也可以一两百万元。比如专业的策划团队会做用户调查、读者功能（报价可以是1000元~10万元）、个性定制（LOGO、个性VI等），而这些如果聘请专业的设计师来做，花费三五万是很正常的，好的设计20万元也不贵。其次，购买网站源程序的版权和个性定制。做网站的程序本来就有免费的，但是免费的没有版权保护，需要购买版权或者购买定制版本，这一个定制可能就会花费2万元~50万元，当然定制后有VIP24小时随叫随到的专门服务，报价会更高。<br />
<br />
　　第三，政府类网站安全涉及国家机密，安全至关重要。尽管对于类似摄影人之家的个人网站来说，源程序出现漏洞了由程序员打补丁，简单修改一下或者安装几个补丁文件就可以，但这毕竟是个人网站。政府类网站要求会更高，比如24小时防范黑客攻击和窃取商业机密，而这一项报价可能达到一二百万元。不仅如此，网站统计可能是这类网站要求比较高的。尽管现在有很多免费的统计软件，但是政府类网站还是希望能够自己设计软件代码，这一项费用也无疑会增加成本。<br />
<br />
　　最后，算上独立的域名、空间和服务器，可能需要在全国甚至更多的地方部署，还需要人力24小时值守，这都涉及到成本，而改版调查、需求反馈等也 需要成本支持。<br />
<br />
　　所以说，建站这种事仁者见仁，智者见智，因为工作量无法衡量，所以劳动成果无法拿来比较。<br />
<br />
　　记者手记&gt;&gt;<br />
<br />
　　阳光需要照到更多的地方<br />
<br />
　　尽管根据已经公开的中标公告，我们并不能对这两个中标网站的具体内容做评估或者是评判，因为我们不知道双方约定的是哪些方面的建设和运营，也不 知道双方的合同年限。引起大家质疑和关注的，只是这两个庞大数字与目前IT行业建站的报价差距巨大。<br />
<br />
　　从2000元就可以搭起一个平台，到3520万元，这是个令任何人都疯狂的数字。专注于企业建站的策划源网络工作室的创始人刘晓东都感叹，对于小公司来说，5万元的建站单子就已经算大单了。然而，我们在中国政府采购网上去搜索就很容易发现，高达数十万甚至上百万的政府网站建设或者维护费用并不鲜见。既然已经可以查到公开的招投标信息，那么关于这些建站项目的费用具体用在什么地方了，是否也能进一步公开呢？<br />
<br />
　　实际上，在互联网的大趋势下，政府借助互联网来处理和展示自己的政务已是迫在眉睫的事情，但是如何让政府类网站建设（包括发标、投标、审评等环节）在公开、公正、透明的原则下，完善流程、加强监督，也是十分关键和必要的。我们只是希望，能够再公开一点，让“阳光政务”能够照到更多的地方。<br />
<br />
　　其他政府网站报价一览<br />
<br />
　　1.国家税务局2009年度机关局域网维护等6个项目成交总金额近500万元，其中网站升级完善项目金额为106万元。<br />
<br />
　　2.中国计划生育协会网站建设一期中标公告显示，成交金额为99万元。<br />
<br />
　　3.最高人民法院政务网站升级改造项目采购结果公告显示，中标金额为144万元。<br />
<br />
　　4.公安信息网门户网站扩容改造项目，最终中标额度为162万元。</div>

							
							
							
							
