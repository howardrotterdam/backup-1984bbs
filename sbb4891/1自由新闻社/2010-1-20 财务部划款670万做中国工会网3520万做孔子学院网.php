&nbsp; &nbsp; 有网民一度反对认为，如此高的招标价格很可能包含了网站运行所需要的服务器、路由器等硬件设备，但记者发现，“中国工会网扩建项目一期工程”事实上还进行过一次专门的硬件招标，其项目编号为ZC-A ZB09085，中标者为两家供应商，中标金额总计超过1200万元。<br />
