													<h2>左右捣腾：国家汉办3520万向下属公司采购网站掀舆论热潮</h2>
						
						
																											<div id="postmessage_445897" class="t_msgfont"><img src="http://img1.gtimg.com/news/pics/27606/27606461.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
有网友发现，“网络孔子学院网站运营服务”项目中标金额为3520万元，中标者为其下属公司。网友戏称此为“史上最贵网站”，质疑招投标属于“左手倒腾到右手”。<br />
<br />
昨日，国家汉办(孔子学院总部)相关人员表示，“巨资”并非仅用于网站建设，还包括后期运营和推广费用，下属公司中标并不违规。<br />
<br />
网友：下属公司中标“最贵网站”<br />
<br />
网友在财政部网站发现，“网络孔子学院网站运营服务”项目中标公告显示，其第1包的中标金额为3520万元，采购人为国家汉办(孔子学院总部)，中标供应商为五洲汉风网络科技(北京)有线公司(简称五洲汉风网络)。<br />
<br />
网友称该网站可谓“史上最贵网站”。根据《政府采购法》第十二条规定，在政府采购活动中，采购人员及相关人员与供应商有利害关系的，必须回避。五洲汉风网络是国家汉办直属机构：“这不是从左手倒腾到右手吗？”<br />
<br />
据悉，国家汉办全称国家汉语对外推广领导小组办公室，与教育部对外汉语教学发展中心和孔子学院总部合署办公，是教育部直属单位。<br />
<br />
中国互联网协会交流与发展中心主任、无限信息服务专业委员会副秘书长胡延平称，做这样一个网络孔子学院网站“任何一个个人站长来做，连10万都用不了”。<br />
<br />
国家汉办：中标价不只包括建设资金<br />
<br />
国家汉办综合处工作人员解释，“网络孔子学院网站”运营服务所需费用，并不单指网站建设所需资金，也包括后期开发和推广等方面的经费。<br />
<br />
“额度是招标得来，并不是预先拟定的。”该工作人员表示，中标的五洲汉风网络确为下属单位，但其参加招标并不违反规定。投标过程依法进行，中标结果合法有效。<br />
<br />
专家：采购信息应细化公开内容<br />
<br />
中国人民大学法学院刘俊海教授认为，政府采购招投标只要相关单位具备资质和条件，都可以参加。需要回避的是“与供应商有利害关系的采购人员及相关人员”，而非采购单位和供应商。<br />
<br />
刘俊海说，目前监管部门对于政府采购信息的公开过于简单。相关政府部门应该细化公开内容，满足公众行使知情权和监督权的需要。<br />
<br />
■ 背景<br />
<br />
中标公司在截标前3月注册<br />
<br />
法定代表人与国家汉办副主任同名同姓<br />
<br />
昨天，凤凰卫视主持人闾丘露薇在博客援引好事者的调查资料称，昨天上午还无法通过搜索引擎和网络工商资料查询到的中标企业“五洲汉风网络科技(北京)有限公司”，昨天下午已经可以被查出，注册资金为600万元，法人代表为王永利。另外一家名为“五洲汉风教育科技(北京)有限公司”，在昨天上午时，注册资本为25万元，下午则变成注册资本200万元，法人代表为胡志平。<br />
<br />
另据记者了解，上述两家公司法人代表王永利、胡志平，均与国家汉办的两位副主任同名同姓。工商资料显示，五洲汉风网络的注册时间是2009年9月9日，仅在孔子学院网站的招标截止时间12月9日之前三个月。（来源：新京报&nbsp;&nbsp;记者段修健 彭梧 ）</div>

							
							
							
							
