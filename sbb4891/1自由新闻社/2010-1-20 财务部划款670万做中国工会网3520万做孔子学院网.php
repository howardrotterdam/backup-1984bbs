													<h2>互联网为保8做出了卓越贡献</h2>
						
						
																											<div id="postmessage_444255" class="t_msgfont">来源 <a href="http://tech.xinmin.cn/2010/01/21/3410494.html" target="_blank">http://tech.xinmin.cn/2010/01/21/3410494.html</a><br />
<br />
　　财政部网站公布的中标公告截图，公告显示，中国公会网此次招标的项目只涉及网站改版，内容管理等功能，不涉及硬件采购部分<br />
<br />
　　本报讯(记者 张东锋)“中国工会网网站改版670万元”，“孔子学院网站运营服务3520万元”。最近两天，财政部网站上公布的两则中直机构的中标公告引来网民围观。做个网站动辄就几百万元的成本支出，让“无比惊诧”的网民惊呼“做个网站好值钱”。<br />
<br />
　　“史上最贵网站出炉”<br />
<br />
　　最先被围观的是财政部网站上招标编号为T C 099R 72的“中国工会网扩建项目一期工程(网站改版、内容管理、站内检索、统计分析)”的中标公告。根据公告，本次招标的成标结果确定于2009年12月25日，成交结果则高达670万元。<br />
<br />
　　“史上最贵的网站出炉”，网友“lcheng”在一家论坛上发帖说。而在凯迪论坛上，另一位网友干脆以“无比惊诧”来描述自己的感受。<br />
<br />
　　有网民一度反对认为，如此高的招标价格很可能包含了网站运行所需要的服务器、路由器等硬件设备，但记者发现，“中国工会网扩建项目一期工程”事实上还进行过一次专门的硬件招标，其项目编号为ZC-A ZB09085，中标者为两家供应商，中标金额总计超过1200万元。<br />
<br />
　　除了中标金额，成交供应商“北京中软宏大信息技术有限公司”的资质也遭到部分网民的质疑。名为“幼学笔记”的博客上，博主称自己特意研究了这家“有能力做670万元‘大项目’的公司”，然而他发现，原以为“信息建设自然应该很牛”的企业的官方网站，居然只能在微软的IE浏览器下正常显示，当使用火狐浏览器打开时，“乱成一团，完全不可用”。<br />
<br />
　　网站改个版、做做统计分析，竟然要上百万元的花费，很多跟帖的网民纷纷替采购人“中国工会网络中心”感到不值。<br />
<br />
　　网站建设 “兄弟”中标<br />
<br />
　　不过，很快有网民发现，“中国工会网络中心”的这次招标显然还不是“史上最贵的”，因为同样出现在财政部网站中标公告中的“网络孔子学院网站运营服务项目(招标编号：CEIECZB01-09JX033)”，第1包的定标日期是2009年12月15日，中标金额则高达3520万元。<br />
<br />
　　根据中标公告，这个名为网络孔子学院网站运营服务项目的采购项目，采购人为国家汉办(孔子学院总部)，中标供应商为五洲汉风网络科技(北京)有限公司。<br />
<br />
　　除了高达数千万元的中标金额，中标供应商也引起了网民的注意。有网民指出，该公司是国家汉办的直属企业。记者在网络上搜索后发现，虽然公司尚无官方网站，但国家汉办官方网站在进行所属机构介绍时明确提到，其十一个组成机构其中之一就是“五洲汉风教育科技(北京)有限公司”。而《中华人民共和国政府采购法》第十二条规定，在政府采购活动中，采购人员及相关人员与供应商有利害关系的，必须回避。<br />
<br />
　　“下属网站招标，直属机构中标”，这也让网民感慨，“怪不得孔子这么牛呢”。<br />
