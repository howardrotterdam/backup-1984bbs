&nbsp; &nbsp; 综上，辩护人认为，郭泉是无罪的；如果，郭泉被判有罪，历史将一定会做出公正评价！像林昭、李九莲、钟海源、张志新、遇罗克那样。我们不希望历史的悲剧重演。&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; <br />
