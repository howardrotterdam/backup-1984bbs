&nbsp; &nbsp; 再次，谁又会响应这个理念？按照三个代表理论，中国共产党都代表全体人民了，谁又会响应郭泉的号召而不上班呢？何况，大家也都是要养家糊口的。辩护人想问，如果郭泉忽然想出一个办法说，要引爆太阳，灭绝全人类，那么是不是也是颠覆国家政权罪呢？<br />
&nbsp; &nbsp; 七日在家革命，理论上涉及的是罢工权，82宪法之前，都规定了罢工权，82宪法虽然没有规定，没有规定的理由是工人都当家作主了，不需要罢工了。这说明罢工权仍然保留在人民手中。只不过无需使用而已。社会在向前发展，如果75年文革中的宪法都有罢工权，说之后人民就失去罢工权了，那么我们国家宣传的进步又是什么？郭泉呼吁大家行使罢工权，如果人民觉得没有需要，就不会响应，如果人民觉得需要，也可以行使。决定权在人民，如何能颠覆国家政权？<br />
&nbsp; &nbsp; 第六，起诉书指控郭泉借维权之名，策动推翻社会主义制度既没有事实依据，逻辑上也是错误的，维权维护的是合法权益，法律是维护国家长治久安的，维权不会推翻社会制度。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;首先，郭泉对维权人士说的，只是希望通过多党竞选，解决他们的合法权益被侵害的事实，如前所说，这种理论并不会导致颠覆国家政权，推翻社会主义制度。<br />
&nbsp; &nbsp; 其次，没有任何客观证据证明郭泉在借维权之名策动推翻社会主义制度。郭泉文章中有时会有些言语涉及到共产党，但是，往往是针对中国共产党的独裁行为，如前面所说，既然，中国共产党没有搞独裁，对着空气打拳，有什么所谓损害？其次，政党不等于社会义制度。起诉书的逻辑也是错误的。<br />
&nbsp; &nbsp; 第三，郭泉帮助军转干部、抗美援朝老兵、城市退伍志愿兵、被买断工龄者、城市被拆迁房屋者、城市被经租房屋者、乙肝病毒携带被歧视者等弱势群体维权，维护的都是他们的合法权益，帮助他们解决问题，只会是帮助社会稳定，而不会颠覆政权。<br />
&nbsp; &nbsp; 第七，根据刑法第105条的规定，颠覆国家政权罪是指：组织、策划、实施者、及积极参加者和其它参加者，应该是组织犯罪（即有组织的多人共同犯罪），郭泉一个人分身乏术，单人根本就无法构成本罪。<br />
&nbsp; &nbsp; 刑法规定组织、策划、实施颠覆国家政权、推翻社会主义制度的，对首要分子或者罪行重大的，处无期徒刑或者十年以上有期徒刑；对积极参加的，处三年以上十年以下有期徒刑；对其他参加的，处三年以下有期徒刑、拘役、管制或者剥夺政治权利。从条文看，郭泉到底是首要分子还是积极参加者，抑或是其它参加者。如果是首要分子，那么其它次要分子是谁？如果郭泉是积极参加者，那么谁是首要分子。因此，以此罪名来指控一个单人犯罪是错误的，逻辑上也无法成立。<br />
&nbsp; &nbsp; 如果说新民党是一个众多党员的旨在颠覆国家政权、推翻社会主义制度的政党，那么为什么今天站在被告席上只有郭泉一个人，其它的党员为什么不被追究刑事责任？<br />
<br />
审判长、审判员：<br />
&nbsp; &nbsp; 任何人都必须为自己的行为负责。历史是后人写就的，法官的职能是依法审判，依照的法律是现行的宪法、法律。我国刑法105条，法律规定非常明确，颠覆国家政权、推翻社会主义制度的，才构成犯罪。郭泉的理论只反共产党的所谓“独裁专制”，提倡全民福利的民主社会主义制度，提倡由人民选择执政党，其行为不满足犯罪构成四要件，完全不构成犯罪。<br />
&nbsp; &nbsp; 根据我国法律，合议庭法官要依据事实证据内心确认被告人郭泉构成犯罪后才能判刑，如果有悖于法，是迫于某种行政的指示或压力做出错误判决，法官对人民、对历史、对自己都是应当负有责任的。<br />
&nbsp; &nbsp; 毋庸讳言，郭泉或许是一个走在时代前面的人，却并没有失去理智，从庭审看，相反条理清楚，曾经也是你我的同行，郭泉的主要理论无非是看得超前、想得超前而已，其行为没有社会危害性。我们在历史教科书中可以看到，任何走在时代前面的人都会受些误解，譬如孙中山作为先行者，当时被满清政府通缉，中国共产党的创建者李大钊、陈独秀被当时政府定罪。历史告诉我们那些将先行者定罪的人，都已经把自己钉在了历史的耻辱柱上。前车之覆，后车之鉴。<br />
