[IMG]http://i47.tinypic.com/2zzj6lz.jpg[/IMG]<br />
国国防部周五（29日）公布了向台湾出售武器的计划，预料将使美中关系再趋紧张。<br />
<br />
这次军售总值超过60亿美元，包括114枚爱国者导弹、60架黑鹰直升机、鱼叉反舰导弹、猎雷舰和防卫通讯设备等，但是不包括台湾要求的F-16战机。<br />
<br />
白宫已经正式通知国会对台军售计划，这是奥巴马行政当局首次出售武器给台湾。<br />
<br />
国家安全顾问琼斯说，华盛顿会与北京以透明的方式磋商任何对台军售计划。<br />
<br />
他还表示，中国是崛起大国，美国很重视对华关系。<br />
<br />
中方反应<br />
北京曾经多次发出警告，美国对台军售会损害中美关系。<br />
<br />
美方承认，北京有可能采取报复措施，中断与华盛顿的军事会谈。布什行政当局2008年宣布向台湾出售价值几十亿美元的武器装备，北京就中止与华盛顿的军事会谈。<br />
<br />
中国驻华盛顿大使馆新闻发言人王保东表示，中国将对美国的决定提出正式抗议。<br />
<br />
当被问到中国是否会中止与美国的军事会谈，王保东说，“让我们等着瞧。”<br />
<br />
他表示，“我们强烈要求美方改正这一错误行动，避免进一步损害中美两国关系。”<br />
<br />
事先未沟通<br />
据中央社报道，美国资深官员周五表示，美国与中国之间有深厚关系，相关对台军售不会影响双边关系。<br />
<br />
官员强调，对台军售是基于台湾关系法，同时中国仍在不断强化对台武力，美国认为军售并无不当。<br />
<br />
官员指出，在正式通知国会之前，美国并未事前与中国沟通，直到周五上午才告诉中国官员，固然中国有其顾虑，但相信不至影响美中关系。 <br />
<br />
美国前总统布什2001年曾承诺要向台湾出售总额达110亿美元的武器，此次军售项目将部分满足之前美方的承诺，但同时也会考验欧巴马政府的对华政策。<br />
<br />
华盛顿许多人士已预料中美军事交流的暂时中断已是无法避免。<br />
