<img src="http://img1.qq.com/xian/pics/17185/17185231.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<a href="http://xian.qq.com/xianent" target="_blank">http://xian.qq.com/xianent</a>　 2009年04月24日10:12 　 腾讯娱乐　 　 评论7条 <br />
<br />
“南京大屠杀”一直是中日之间的敏感话题，每当国际上出现与此相关的影视和文学作品，日本都会十分紧张。22日，力图真实还原南京大屠杀历史的中国影片《南京！南京！》在中国公映，日本媒体立即作出反应，担心“日本的行为被夸大”，“日本的形象因此受到破坏”。由于电影公映恰好在日本首相麻生太郎访华前夕，并且麻生最近还向靖国神社献祭品，日本国内有人担心，“这一连串的动作，有可能在中日之间引起新的火种”。 <br />
<br />
日本《产经新闻》22日发表名为“强调残虐……中国电影公映，暴行、杀戮的场面让观众发出悲鸣”的文章。文章不无埋怨地称，“以所谓的南京虐杀事件为主题的电影《南京！南京！》于本月22日在中国各地公映。虽然围绕着该事件，日本和中国的认识有所不同，但这部电影全部都是黑白片，使用了模拟纪录片的方法，给观众造成好像电影的内容都是真实的印象”，“特别突出了暴行和残虐的场面……当看到对女性施暴和残杀大众的场面时，观众席上发出悲鸣和激烈的怒骂声”。 <br />
<br />
《产经新闻》进一步称，29日，由中法德合拍的以南京虐杀事件为题材的电影《拉贝日记》也要公映。此外，5月4日还是中国大规模反日运动的起点“五四”运动90周年纪念日。“因此，日本一些人士神经紧张，认为这一连串的事件很可能成为日中之间新的火种”。 <br />
<br />
日本《每日新闻》在报道此事时也特意强调了“敏感”二字。这篇名为“以南京为主题的电影在中国全国接连不断公开”的报道写道，“这是一个日中之间敏感的话题，而且公映时间又正是中国历史上爱国反帝运动的起点---五四运动90周年纪念日，这令此类题材的电影很可能成为日中之间新的火种”。 <br />
<br />
相比之下，雅虎网站日本频道介绍了《南京!南京！》一片的日本演员在中国受到中国观众致谢一事，并称这并不是一部“反日一边倒”的作品，也融入了日本演员的视点和意见。日本共同社则称，《南京！南京！》尚无在日本上映的计划。目前，反映南京大屠杀题材的电影《拉贝日记》在日本也被禁演。但在日本民间，有人在网上发起了要求在日本国内公映这些影片的联合签名运动。 <br />
<br />
事实上，日本首相麻生21日向靖国神社献祭品的举动也确实加重了日本媒体的这种担心。21日和23日，中国政府连续告诫日本政府要慎重对待历史问题。23日，中方的措辞更加严厉。中国外交部发言人指出，中方已通过外交渠道向日方表明严重关切和不满，强调历史问题高度敏感，日方任何错误举动，都将对两国关系带来严重消极影响。对此，日本《朝日新闻》称，中国政府再次公开声明，表示严重关切和不满。而中国网民对麻生的批评声音更强烈，甚至主张不要让其访华。 <br />
<br />
中国社科院日本研究所研究员高洪认为，从日本媒体对待《南京!南京!》电影的态度上可以看出，一些媒体想要文过饰非，但最终的结果是欲盖弥彰。高洪说：“日本媒体认为这部电影会勾起两国人伤痛的回忆，引发新的摩擦。仅这一点就说明两国在对历史问题的认识上还有相当大的差异。如果日本对那段侵略历史发自内心的反思和反省，必然不会害怕别人去触碰它。日本这种鸵鸟心态无益于问题解决，也不利于中日两国关系向前走。” <br />
<br />
高洪向《环球时报》记者表示，中日之间和解的路还很漫长，尤其是对日本来说，真正的和解是民间层面和心灵层面的和解。</div>

							
							
							
							
