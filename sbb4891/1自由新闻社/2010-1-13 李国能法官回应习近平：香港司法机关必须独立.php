张文光s三权合作的一盆冷水 <br />
<br />
【明报专讯】李国能最后的法律演说，全场起立鼓掌一分钟。掌声既来自法律界，也发自香港的人心。<br />
<br />
李国能确认香港政治体制的三权制衡，与中央领导人强调的三权合作，泾渭分明，掷地有声。<br />
<br />
香港回归13年，受到一国大潮冲击，仍如灯塔般屹立不倒的唯有司法。习近平、吴邦国、张晓明3次对香港的发话，指桑骂槐，意有所指，都指向学习澳门的三权合作。<br />
<br />
香港行政主导，立法残缺，制衡力已如鸡肋，若司法权也被河蟹，三权制衡如骨牌沦陷，香港先澳门化后中国化，一国两制将荡然无存。<br />
<br />
李国能的临别赠言，尤其对澳门的三权合作，作出毫不含糊的拒绝：香港和澳门均属一国两制的特区，但澳门根据前葡萄牙政府订下的法律框架运作，与香港履行普通法不同，澳门的法制完全不适合香港。<br />
<br />
李国能：香港应坚持司法独立<br />
<br />
李国能的结论是：香港应坚持司法独立，三权互相制衡，才能保障市民权利及自由。若李国能的讲话，说于97回归时，或者平平无奇；但说于13年的司法风雨后，却是弥足珍贵，历史立此存照。<br />
<br />
真民主不止于普选，而在于权力的制衡。三权分立的司法机关，虽然入世仍属清流，李国能说：法官的职责是执行司法工作，无惧无偏。若行政或立法机关胜诉，这不是因为法庭有意偏袒。同样，若判行政或立法机关败诉，也不是因为法庭有意对抗。<br />
<br />
无惧无偏，才能制衡；心存合作，志在和谐，迟早成为行政权力的尾巴，像中国的法庭重判刘晓波，世上有多少人相信其司法独立？像中央推许的澳门三权合作，《新澳门》也有文章〈专制国家才会三权互相配合〉指出其祸害：<br />
<br />
「三权配合政治体制，造就利益输送不断。立法会成了典型的橡皮图章，不能有效制衡和监督；更造成行政霸权、官权独大、吏治和司法腐败，巨贪欧文龙案由此产生，而且极可能只是冰山一角。由此可知，澳门可说没有一项值得学习的。」<br />
<br />
三权分立的权力制衡，是人类历史累积的智慧。1793年，美国立国初期，三权分立不清，总统华盛顿委托国务卿杰斐逊，以联邦政府的名义，写信给联邦法院的大法官，咨询他们对29个法律问题的意见。<br />
<br />
法官没有因而感到厚爱，反而写信给华盛顿说：宪法为政府互相制约的三个部门划出分界线，我们作为最后倚仗的法庭法官，有强烈的理由认为：不由自主地参与超越法庭职权问题的决策，是不适当的。进一步而言，宪法赋予总统召集各部门首长征求意见的权力，明确地仅限于行政部门。<br />
<br />
这是200多年前，美国法官的超前智慧，突显三权分立的精义，确立司法机关的独立，连行政机关的咨询都不卖帐。<br />
<br />
发人深省的司法故事，是三权合作的一盆冷水。</div>

							
							
							
							
