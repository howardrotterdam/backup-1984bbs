													<h2>李国能早退真相 曾荫权应辞未辞</h2>
						
						
																											<div id="postmessage_431424" class="t_msgfont">信报| 李国能早退真相曾荫权应辞未辞(林行止) <br />
 2010年1月13日 <br />
<br />
林行止 <br />
李国能早退真相 曾荫权应辞未辞 <br />
<br />
一、 <br />
<br />
终审法院首席法官李国能周一主持任内最后一次亦是第十三次法律年度开启典礼，他的演词，掷地有声，在京官鼓吹「三权（行政、立法和司法）合作」逆流中高举维护「三权互相制衡」大旗，所有的香港人都应仿效与会者（包括公务员及政治任命官员）以起立为这番说词鼓掌逾一分钟的致敬方式，向李大法官敬礼；在尊重法治的社会，应考虑于终审法院门前为李国能立像，以表彰他维护法治之功及体现承传之志；但在「三权合作」的阴影下，有这种可能吗？ <br />
<br />
李国能的演说以及会后答记者问，揭示了他「提前退休」的根本因由。请重复看看他怎样解释香港现行法制的运作U「法官处理涉及行政或立法机关的案件时，既不采取对抗态度，亦不刻意偏袒任何一方。法官的职责是执行司法工作，无惧无偏。若行政或立法机关胜诉，这不是因为法庭有意偏袒；同样，若判行政或立法机关败诉，也不是因为法庭有意对抗。无论判决如何，法庭都只是履行宪法职能，公平公正地审理案件。」这种坚持，不仅令「没落中的香港」仍能吸引大批内地移民，亦是香港金融业依然蓬勃兴盛投机家尚未弃香港而去的底因！事实上，在这里生活过的人都清楚了解，「三权互相制衡」是香港尤其是回归十二年来能够享有自由、公平和法治的基石！ <br />
<br />
值得特别关注的还有，李大法官对国家副主席习近平以次的内地负责港澳事务官员「指示」香港应学习澳门法制的回应U「澳门的法制完全不适合香港」。简单明了、斩钉截铁，这是对权威的挑战，尽显其司法独立「无惧无畏」的本性！ <br />
<br />
在谈及司法人员委任时，李国能进一步否定「三权合作」的合适性U「司法（人员）任命过程绝不应政治化，这一点对司法独立，至为重要。」政治一旦介入司法人事布局，等于香港司法（以至律政）「新加坡化」。不必讳言，在「党本位」挂帅之下，只能听命于党的三权一旦合作，还有什么公平公正的法制可言……。不过，尽管本地民意、世界舆论以至大部分司法界人士都确认「三权」必须独立才能「互相制衡」，方为保持香港生活于不变的正道，但中国已具富国强兵的格局，自豪感自信心特强，当权者因此均认为「现在的制度很好，要巩固它」。中国哲学家李泽厚认为这是「危险讯号」（FT中文网一月四日）；如今的情况是，不但要巩固它，还有意把之引进香港。中国各方面都突飞猛进，当政者以为「现行制度很好」，理所当然，反对之声皆属噪音；<strong>笔者相信李国能之后不久，「三权合作」会慢慢在香港生根。 </strong><br />
<br />
如果「三权合作」渐成主流，李国能的「徒弟」、律政司司长黄仁龙的工作将倍加艰难，黄氏周一在同一场合中赞扬「师傅」在「成功落实『一国两制』原则及设立终审法院这些历史性任务上居功至伟」外，尚提醒北京领导人U「司法独立是香港法制的基础」；又说「社会不应把本质属政治或经济问题的案件压在法庭的肩膊…… 」。可圈可点，而「三权合作」一旦推行，相信很快便会把「法庭的肩膊」压扁！ <br />
<br />
二、 <br />
<br />
行政长官曾荫权在香港政制改革上「壮志未酬」，连累他那份工，在大多数港人看来，做得不够好！二○七年三月他争取连任时说过在政改上要「玩铺劲」（《文汇报》稍后的解释是「来一次厉害的」），当年他也许有此「宏愿」，但发展下来，政改有退无进，曾氏当然把这种现象归咎于「反对派」的「破坏」，但「反对派」为什么反对而且获得广泛民意支持，是曾氏应自省的问题。 <br />
<br />
政改无成，不等于曾氏不曾向北京争取，更大可能是「屡争无效」，因为北京一定要按照其原则其方式其进度办事，结果京意与港意在政改进度上分歧愈来愈大，香港社会亦因此愈来愈分化愈不和谐。在这种情形下，曾氏应否请辞？机会成本不同的人有不同的答案。 <br />
<br />
非常明显，曾氏并无去意，这也许是他的责任感使命感以至荣誉感，令他忍辱负重，继续为「港人服务」。但问题是政改并无寸进，等于他无「重」要负，「忍辱」的目的因此与政改无关，那又是为了什么呢？退一步看，曾氏也许一看声色不对根本未把港人所要的政改上呈，果如是，他当然更没有辞职的理由。 <br />
<br />
可是，当国家领导人指示要香港学澳门的时候，他应该请辞吧，别说香港缺乏澳门黄赌毒贪的硬体建设，作为一个虔诚（如果天天上教堂等于虔诚）的天主教徒，在道德上曾氏如何能带领香港澳门化？换句话说，在他的领导下，香港根本无法学澳门，有违国家期待，等于无法完成领导人交下的任务，曾荫权何以仍恋栈不去？</div>

							
							
							
							
