<br />
&nbsp; &nbsp; 我不由得想起来我的一个朋友，从南京去新疆生活了很多年，前段时间在QQ上一直给我发图片，盛赞新疆取得的巨大发展成就，他很自豪地告诉我说：我们新疆现在可好了，乌鲁木齐现在也是大城市了！<br />
<br />
&nbsp; &nbsp; 我有一个在南京上学的维族舍友，虽然没见过面，但每次给我留言，他都跟我介绍家乡的美景，诚恳地邀请我有空去玩，我喜欢新疆，无数的文艺作品里出现过的新疆，是那么具有吸引力，所以在我的日程安排里，去新疆是非常重要的一个愿望。<br />
<br />
&nbsp; &nbsp; 我从小虽然不在民族聚居区生活，但从我所受的教育来看，新疆带给我们的是多么美好的感觉，那里生活着非常热情的维族同胞，那里有聪明可爱的阿凡提，那里有葡萄和哈密瓜，那里有石油，有博斯腾湖，有天山，有雪莲，有手抓羊肉，有大盘鸡-----我们从小就对新疆充满了好感，对维族同胞充满了亲近。那些分裂分子说中国实在用民族迫害政策来对待新疆各族人民，我想质问他们：如果我们一直在迫害民族同胞，那么怎么会对这片土地倾注了如此多的感情色彩？如果我们在迫害维族同胞，那么请问为什么维族的舞蹈和美食可以全国开花？如果我们真的在迫害维族同胞，那么为什么新疆的经济发展得这么好这么快？？<br />
<br />
