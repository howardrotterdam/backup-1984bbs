是学习金日成同志的革命历史，深入认识金日成同志的英明伟大，把自己造就成为朝鲜革命的栋梁。<br />
1953年7月，朝鲜人民的祖国解放战争取得了胜利。战后，金正日同志从平壤第四小学毕业，升入平壤第一中学，努力学习多方面的知识。<br />
1957年9月1日，金正日同志升入平壤第一中学高级班(后来改称平壤高山高级中学)。在校期间，他经常和同学们一起到工厂、农村去，并在学校实习室里参加修理汽车、电动机等劳动，还学习驾驶汽车，努力掌握这方面的技术。<br />
金正日同志在学生时代就积极参加民主青年同盟的工作。<br />
1957年2月，他当选为民主青年同盟学校基层组织的委员长，1957年9月又当选为民主青年同盟学校委员会副委员长(委员长由教师担任)。<br />
1960年8月，金正日同志从高中毕业。<br />
1960年9月1日，金正日同志考入金日成综合大学经济第政治经济学专业。<br />
大学时代，他不仅掌握了专修课政治经济学的理论，还掌握了哲学、军事学等各个领域的多方面知识。大学时代，他一面探求科学，一面开展各种活动。<br />
金正日同志在平壤纺织机械厂实习期间，亲自操作26号机床。<br />
1961年7月22，金正日同志加入了朝鲜劳动party。<br />
大学时代，金正日同志发表了《重新查考三国统一问题》、《关于现代修正主义的产生》、《关于思想意识的决定性作用》（1962年12月）、《工人阶级的领袖并不是个人》、《工人阶级的领袖在革命斗争中起决定性作用》、《我们party关于发展地方经济方针的正确性》、《大安工作体系是独创的社会主义经济管理体系》等一系列理论著作。<br />
1962年1月，他发表了以《现代帝国主义的特点和侵略本性》为题的学科论文。<br />
1964年3月18日，金正日同志发表了大学毕业论文《郡在社会主义建设中的地位和作用》。<br />
金正日同志从1964年6月19日起在party中央委员会工作。<br />
自1965年春到1967年夏，他视察人民军航空部队、东海岸和西海岸海军部队以及许多炮兵分队，指导他们扎实地进行战斗训练。<br />
他非常关心改善人民军的武器装备，指示军需工业部门大力生产适合朝鲜实际和朝鲜人体质的有威力的现代化武器。<br />
1967 年6月末和7月初，他多次接见party中央委员会宣传鼓动部和文学艺术部门的负责干部，就作家、艺术工作者和新闻出版工作者切实树立party的唯一思想体系问题作了教导。1967年7月17日~8月12日，他到咸镜南道进行现场指导，通过召开party内思想斗争会，批判修正主义、地方主义和家庭主义，把在思想斗争中激发起来的革命热情，有力地引导到经济建设中去，掀起新的生产高潮。<br />
<br />
1967年7月，他通过同人民军某部干部的谈话和后来对人民军各部队的多次现场指导，了解到窃据军队要职的军阀官僚主义者以“军队特殊”为由，拒绝party对军队的领导，歪曲party的军事路线，滥用军权压制军内party组织和政治机关，而且企图纠集阿谀奉承分子来奠定其军阀官僚主义基础，甚至还摄制宣传自己的电影，妄图抬高自己。<br />
1967 年6月13日，金正日同志在同party中央委员会科学教育部干部的谈话《对精神鼓励和物质鼓励要有正确的认识》中，批判了把物质鼓励视为绝对正确的修正主义倾向，把精神鼓励视为绝对的左倾倾向；并对以精神鼓励为主，把正确地结合以物质鼓励的问题和经济建设中必须把速度放在首位，同时积极保持平衡的问题等社会经济建设中的理论与实践问题，作了科学解答。<br />
<br />
1967年7月3日，他在同party中央委员会宣传鼓动部干部的谈话《加强思想宣传，在经济建设和国防建设中掀起革命高潮》中强调指出，所有干部和劳动者要高度发扬自力更生、艰苦奋斗的精神，最大限度地增加生产，厉行节约，促进技术革命，更积极地开展千里马作业班运动。<br />
<br />
他亲自深入到劳动者中去，发动party员和劳动者掀起新的革命大高潮。<br />
1967年8月8日，他到龙城机械厂，向工人解释party的关于掀起革命大高潮方针的本质和意义，号召他们站到革命大高潮的前头。<br />
1967年8月，他视察长津江发电站，同年9月视察平壤热电厂，1968年9月视察北仓火电厂建设工地，推进发电厂（站）建设，增加发电能力，以建立起国家牢固的动力基地。<br />
他还到黄海钢铁厂等冶金厂、机械厂、化工厂和平壤、咸兴、龟城等地的轻工业工厂进行现场指导，帮助<br />
他们有效地解决了加强工业的自立性，促进现代化中的问题。<br />
与此同时，他还为了在工业部门积极推进技术改造，到基干工业的许多工矿企业和大馆郡等市、郡的地方工业工厂进行现场指导，促使这些厂进行技术改造，以实现机械化、半自动化和自动化。<br />
他为了使大安工作体系充分发挥作用，于1967年10月此后多次视察大安电机厂，使其在推广大安工作体系方面做出榜样； 1968年4月，他指导物资供应委员会，改进物资供应工作。<br />
1967年，他组建担负这项工作的白头山创作团，使他们通过故事片《游击队五兄弟》等革命传统题材作品的创作，积累经验。<br />
1968年4月，他提出了要把《血海》改编为同名电影的任务，并进行了120多次指导，终于完成了制版任务；接着还指导了把《一个自卫团员的遭遇》改编为同我电影的工作，仅用了40天就出色地 完成了这项工<br />
作。同时，他还积极指导把《卖花姑娘》改编为同名电影的工作，并使该片成为杰作，在第18届国际电影节上荣获特别奖和特等奖章。<br />
1968年7月和1969年8月，他视察了两江道一带的革命战迹地，提出了把普天堡、三池渊、浦坪等两江道一带建设成为牢固的革命传统教育据点的任务。<br />
1970 年6月18日，他在同作家和艺术工作者的谈话《大量创作反映社会主义现实的革命影片》中，提出了要创作出更多的以现实为的电影的任务，全面地阐明了关于这项任务的理论与实践问题，并给予了具体指导。结果仅在1970年内，就创作出了人民奖桂冠作品《鲜花盛开的村庄》等数十部优秀作品。<br />
金正日同志曾任party中央指导员、科长，1970年9月任party中央委员会副部长，1973年7月任party中央委员会部长，同年9月担负了party中央委员会书记的重任，1972年10月被选为party中央委员会委员。<br />
1973年6月他抽调party、经济、新闻出版部门的干部和艺术工作者组成生产鼓动队，派到载宁矿山、殷栗矿山和苔滩矿山去开展生产鼓动工作，同年11月又在金星拖拉机厂和胜利汽车综合厂开展大规模的生产鼓动工作，使其成为生产鼓动工作的典范。<br />
他将通过思想理论活动深化和发展的文艺思想理论与方针、在领导文学艺术革命的过程中取得的成果和经验综合归纳起来，于1973年4月撰写并发表了光辉的巨著《电影艺术论》。<br />
1973年6月23日，金日成同志提出了新的统一祖国五点方针。统一祖国五点方针，是根据当时的形势把统一祖国的三项原则具体化，给内外主义者的民族分裂阴谋以学生打击，向国内外显示了全民族坚定不移的统一意志的宣言。<br />
他深切关注创造有得于统一祖国的国际环境的工作，积极开展外交活动；1973年6月和7月提出了有关设立驻联合国办事处和通过联合国开展外交活动的问题。<br />
为此，1973年9月举行的第4次不结盟国家首脑会议等国际会议和国际组织会议，通过了积极支持统一祖国三项原则和五点方针的决议和宣言；1973年下半年举行的第28届联大通过决议，支持统一祖国三项原则，要求立即解散美帝国主义干涉朝鲜内政的工具－“联合国韩国统一复兴委员会”。<br />
他洞察到party在实现全社会主体思想化中的地位和作用，于1974年8月2日提出了实现全party主体思想化的方针。<br />
1974年2月，在party的五届八中全会上，他提出了在社会主义经济建设中提前完成六年计划，实现新的远景目标的社会主义大建设纲领。<br />
由于金正日同志遵循金日成同志的意志，在party务工作等革命和建设的各个领域建树了巨大业绩，1975年2月15日，朝鲜民主主义人民共和国中央人民委员会授予他朝鲜民主主义人民共和国英雄称号。<br />
他把切实贯彻《社会主义教育提纲》看作改进和加强教育工作的最重要的问题，1977年10月提出了贯彻教育提纲的思想转变方针。<br />
1978年12月15日，金正日同志提出“按照我们自己的方式生活”的口号，要求按照我们自己的方式推进革命和建设。<br />
1979年5月10日，他指示全party就树立party的领导体系问题，组织集中学习、集中讲习和大辩论；加强各级party委会，增强其职能与作用，以加强party对权力机关工作、行政经济工作、公共安全专家及司法检察工作的领导。<br />
1979年10月，他发起向默默无闻的英雄学习的运动，并把这一运动作为party的重要方针提了出来。<br />
1981年10月，他提出绝对忠于领袖的光荣传统。<br />
1982年10月17日，他发表了题为《朝鲜劳动party是继承了光荣的打倒帝国主义同盟传统的主体型革命政party》，进一步深化和发展了主体的party的建设理论。<br />
从1982年2月起，他连续当选为第七届、第八届、第九届最高人民会议议员。<br />
1984年7月22日，他在致全国教育工作者积极分子大会与会者的信《进一步发展教育事业》中，提出了教育革命方针，并领导了贯彻这一方针的工作。<br />
到了80年代后半期，他继续不断地进行了深化和发展主体思想的理论活动。<br />
他在《主体思想教育的若干问题》（1986年7月15日）、《切实树立主体的革命观》（1987年10月10日）、《对主体哲学要有正确的观点和理解》（1990年10月25日）、《主体哲学是独创性的革命哲学》（1996年7月26日）等许多著作中，以独创的思想与理论进一步发展和丰富了主体思想，论证了主体思想的生命力。<br />
1988年3月，他召开party的六届十三中全会，提出了发展科学技术三年规划（1988～1990），同年8月31日在科学部门负责干部会议上，强调了全面发展电子工程学、冶金工程学和激光工程学等重要科学领域的问题。<br />
金正日同志的文学艺术造诣很深，知识渊博，又有领导文艺革命的丰富经验，于80年代末和90年代前半期撰写了许多文学理论著作，其中有《论话剧艺术》（1988.4）、《论舞蹈艺术》（1990.11）、《论音乐艺术》（1991.7）、《论美术》（1991.10）、《论主体文学》（1992.1）。这些著作和70年代发表的《电影艺术论》、《论歌剧艺术》，都是指明主体文学艺术发展道路的教科书。<br />
1991年12月24日他在party的六届十九中全会上被推戴为朝鲜人民军最高司令官，1992年4月20日被授予朝鲜民主主义人民共和国元帅称号。<br />
1990年5月在第九届最高人民会议第一次会议上被推戴为朝鲜民主主义人民共和国国防委员会第一副委员长，1993年4月9日在第九届最高人民会议第五次会议上被推戴为国防委员会委员长。<br />
1992年4月，他在平壤同向往社会主义的世界70个进步政party的首脑及代表进行诚挚的协商，建立了密切的联系，制定并发表了平壤宣言：《拥护和发展社会主义事业》。<br />
1994年10月20 日，他在同party中央负责干部的谈话《加强郡的作用，使人民生活来个变化》中，要所有的郡都学习前川郡商业管理所和孟山郡的经验，办好农业，发展地方工业，搞好郡人委所在地和村庄的建设。<br />
1994年10月16日，他在party中央政治局会议上，根据金日成同志的遗志，就促进革命和建设问题表示决心说：“我想几百、几千年以后，也要始终不渝地把领袖在领导革命和建设的过程中所作的历史性教导，当作唯一的指导方针加以贯彻。”<br />
进入90年代后半期，由于帝国主义联合势力和反动派疯狂地扼杀主体社会主义的阴谋活动和多年的自然灾害朝鲜人民贯彻革命的经济战略的斗争经受了严峻的考验。<br />
为了贯彻party的革命经济战略，在社会主义建设中掀起新高潮，他教育全体人民以“艰难的行军”精神去生<br />
活和工作。“艰难的行军”精神，是在抗日革命斗争最严峻的时期，在金日成同志的领导下，坚守朝鲜革命的命脉，掀起革命大高潮的透彻的革命精神和百折不挠的革命精神。<br />
1997年6月19日，他发表了题为《在革命和建设中坚持主体性和民族性》的著作。<br />
1997年8月4日，他发表了题为《切实贯彻伟大领袖金日成同志关于统一祖国的遗训》的著作。<br />
1997 年10月8日，根据全party共同意志，金正日同志被推举为朝鲜劳动party总书记。30多年来，他通过夜以继日，废寝忘食的革命活动，将朝鲜劳动 party加强和发展成为百战百胜，久经考验，千锤百炼的革命的party、受到全体人民绝对支持和信赖的战无不胜的party；把朝鲜人民培养成为具有坚定的信念和意志的自主的人民!</div>

							
							
							
							
