													<h2>储君忆插队：扛200斤麦子十里山路不换肩！</h2>
						
						
																											<div id="postmessage_337111" class="t_msgfont">他，一位中国最基层的单位DD当年的生产队书记、县委副书记，省委书记，一步步扎实地走到今天成为国家副主席。 <br />
<br />
他，性情谦和，为人平易，然而执政颇有铁腕风格。 <br />
<br />
他，出身于高干家庭，其父习仲勋是党和国家领导人。 <br />
<br />
政绩为谁而树？树什么样的政绩？靠什么树政绩？在习近平意在深处，谋在新处，干在实处…… <br />
<br />
不忘“一饭之恩”的大丈夫 <br />
<br />
习近平的童年在父亲的严格要求下度过，而他的少年时代更与父亲的命运紧密相连。由于“文革”期间父亲被打倒，不到16岁的习近平就被迫前往陕北插队。这段经历成为他永远难忘的记忆，也成为他最宝贵的一笔人生财富。“我几乎那一年365天没有歇着，除了生病。下雨刮风我在窑洞里跟他们铡草，晚上跟着看牲口，然后跟他们去放羊，什么活都干，因为我那个时候扛200斤麦子，十里山路我不换肩。” <br />
<br />
当时，约29000北京知识青年到延安插队，习近平是第一个当大队支部书记的。这种历练，让他“看到了人民群众的力量，看到人民群众的根本，真正理解了老百姓，了解了社会，这个是最根本的。很多实事求是的想法，都是从那个时候生根发芽的，以至于到现在每时每刻影响着我”。 <br />
<br />
“我的成长、进步应该说起始于陕北的七八年间。最大的收获有两点：一是让我懂得了什么叫实际，什么叫实事求是，什么叫群众。这是让我获益终生的东西。二是培养了我的自信心。这几年‘上山下乡’的艰苦生活对我的锻炼很大，后来遇到什么困难，就想起那个时候，在那样困难的条件下还可以干事，现在干嘛不干？你再难都没有难到那个程度。一个人要有一股气，遇到任何事情都有挑战的勇气，什么事都不信邪，就能处变不惊、知难而进。” <br />
<br />
1968年，习近平从北京到梁家河村插队，习近平不会做饭，吕侯生就把做饭的事“承包”了。习近平酷爱学习，常在煤油灯下读着吕侯生连书名都认不得的书，吕侯生就憨憨地坐在油灯的一旁，陪着习近平看书写笔记。晚上也不回去了，一条被子两人合伙盖。 <br />
<br />
1994年，吕侯生右腿患了骨髓炎，在医院治疗两个多月，花了6000多元仍不见好转。本来就贫困的吕侯生已债台高筑，实在没法了，给习近平写信说了自己的状况。不到半个月，习近平给他寄来500块路费，请他到福州治病。此后，只要在市里，习近平几乎每天晚上都到吕侯生的病榻前来一趟，他说：“侯生，给你治病，都是我自己花钱，花多少我都愿意。” <br />
<br />
经过治疗，吕侯生的病得到了好转。吕侯生要回陕北了，习近平给他买了一张飞机票，还把2000块钱塞到吕侯生手里。吕侯生激动地说，近平同志，真不好意思呀，这一趟花了你几万块。习近平说：“谁让我们是朋友哩！” <br />
<br />
1999年10月底，吕侯生在山西太原武警医院做了截肢手术，习近平闻讯后，将医疗费全部支付。 2000年1月，吕侯生第三次到了福州，这时习近平已当选为福建省的省长。吕侯生是戴着假肢一瘸一拐去的，一见面，习近平用手反复摸着吕侯生的右腿，高兴地说：“我的农民朋友大难过去了，咱一块合影做个纪念吧。” <br />
<br />
2002年“七一”前夕，时任福建省省长的习近平托人给居住在莆田市仙游县榜头镇的83岁老战士李朝金送去慰问金。饱受癌症折磨的李朝金激动地说：“为穷人求解放，多少战友牺牲了生命，我只不过出了一点力，受了几次伤。党和政府这么关心我，让我感动不已。” <br />
<br />
给腐败成本细算“三笔账” <br />
<br />
1921年8月初，中国共产党第一次全国代表大会在浙江嘉兴南湖的一条游船上胜利闭幕，庄严宣告中国共产党的诞生。这条游船因而获得了一个永载中国革命史册的名字DD“红船”。2002年10月，习近平调任浙江后，怀着无限崇敬的心情，专程到嘉兴南湖瞻仰红船，接受革命精神教育。他说，如果我们的党员同志能够来到南湖看一次展览，听一次党课，学一次党章，观一次专题片，瞻仰一次红船，重温一次入党誓词，有促于“精神传承、思想升华”。对习近平，一般在职的机关干部的普遍印象是，“习书记的讲话，往往一语点中要害，对于丑恶现象，从来不留情面”。知情人认为，习近平的个性更在于他强调和重视“官德”和人格魅力。对待“吏治”与“官德”，习近平在浙江可谓措施迭出，警告频仍。 <br />
<br />
领导干部要做到“权为民所用”，就必须法德并举，既要依法用权，又要以德用权，归根到底用权要讲官德。这是习近平对权力的诠释。在他眼里，德，不仅是“立身”之本，而且是“立国”之基。中国历代思想家既重视以德修身，也重视从政以德。“为政以德，譬如北辰，居其所而众星共之。”《论语》中的这句话，他十分推崇。习近平说，“为政以德”，即执政者要以德施政，善待民众，以赢得百姓的拥护；二是要以自身的道德行为去教育和感化百姓，“政者，正也。子帅以正，孰敢不正”DD要想达到“正”，就要自省、自律、自责、克己，通过道德修养提高自身素质。 <br />
<br />
“红船精神”是鞭策我们牢记立党为公、执政为民的本质要求和全心全意为人民服务的根本宗旨，求真务实、一心为民的强大道德力量。习近平说，它直接反映的是个人的“内功”，即个人的道德素质DD道德素质好，就能不论白天黑夜、明里暗里，都能严防死守，矢志不渝，管住自己的口、自己的手、自己的腿，以及自己的亲属和身边工作人员，从而使自己坦荡从容。 <br />
<br />
2004年7月15日下午举行的浙江省推进党风廉政建设电视电话会议上，时任浙江省委书记的习近平代表省委常委会向全省人民作出6条廉政承诺时，语重心长地告诫与会干部要算一算“经济账”、“法纪账”、“良心账”。习近平说，每个领导干部只有想清楚、算明白了这“三笔账”，才能真正行使好人民赋予的权力。大家要按照省委全体常委的承诺要求，坚决抵制跑官要官；坚决拒收钱物；自觉管住配偶、子女、亲友和身边工作人员，决不以权谋私；带头坚持“两个务必”，始终保持公仆本色；带头遵纪守法，自觉接受监督；严格执行党风廉政建设责任制。<br />
