													<h2>八九学运领袖李禄可能接掌巴郡</h2>
						
						
																											<div id="postmessage_765449" class="t_msgfont"><a href="http://inews.mingpao.com/htm/INews/20100730/ca51210p.htm" target="_blank">http://inews.mingpao.com/htm/INews/20100730/ca51210p.htm</a><br />
<br />
八九学运领袖李禄被指可能接掌巴郡，《华尔街日报》详述了他流亡美国后，在美国金融界崛起之路。<br />
报道说，李禄拒绝讨论在巴郡的可能任职，只是说能成为巴郡内部圈子的一分子，他感到很幸运。他说，这是你做梦也想像不到的。<br />
<br />
李禄出生于1966年文革开始的那一年。他说，他9个月大时，他当工程师的父亲被送到了一个煤矿接受「再教育」。他的母亲被送到了一个劳改所。<br />
<br />
李禄的父母向很多家付钱，希望他们照顾他。有好几年，他都在好几个家庭之间辗转，直到到了家乡唐山一个文盲矿工的家里。他和这个矿工建立了深厚的感情。李禄说，小小年纪就与家人分离，这教会了他生存的技能。<br />
<br />
十岁后，他与父母和两个兄弟重新团聚。当时他的家乡唐山发生了一场大地震，造成约24.2万人遇难，其中包括照顾他的矿工一家。他说，他们家在地震中免，不过他认识的大部分人都死了。<br />
<br />
李禄说，当时他没有方向，在街上打架斗殴。他说，他的祖母激励他开始读书学习。他的祖母是她们市里第一批女大学生之一。李禄后来上了南京大学，专业是物理。<br />
<br />
1989年4月，他到北京天安门广场遇到了在那里集会悼念胡耀邦的学生。李禄参与了学生的组织工作，并参加了绝食。<br />
<br />
他和其他学生后来逃往法国。1989年底，他到了美国，在哥伦比亚大学发表演讲。那里的人权活动人士像迎接英雄一样地迎接了他。他不太会说英语，不过却收到了一笔写书的预付款，书的内容是关于他自己经历的。<br />
<br />
在哥伦比亚大学奖学金的帮助下，李禄迅速学会了英语。据哥伦比亚大学说，他成了该校第一批同时获得三个学位的学生之一：经济学、法学和商业硕士学位。<br />
<br />
在李禄的学生贷款不断增加之际，1993年他听了巴菲特在哥伦比亚大学的一次讲座。当时，90年代的牛市正如火如荼，对冲基金在上升阶段。李禄说，在中国，他不相信金融市场，不过听过巴菲特的讲座之后，帮助他克服了对股市投资的质疑。<br />
<br />
他开始用写书的预付款投资股市。1996年毕业前，他已经有了相当规模的储蓄，他说他觉得自己可以退休了。然而，他接受了证券公司Donaldson Lufkin &amp; Jenrette的一份工作，之后离职创建了自己的对冲基金。<br />
<br />
1997年，他创建了对冲基金Himalaya Partners。之后，他建立了一b风险投资基金，以便投资美国的科技公司。<br />
<br />
当时正值华尔街令人迷醉的时候。互联网热刚刚开始。投资者们吵着找热门股。<br />
<br />
通过他与人权人士的关S，李禄迅速吸引了包括Bob Bernstein和音乐家Sting在内的富有客户。<br />
<br />
Bob Bernstein是兰登书屋(Random House)前董事长，也是「人权观察」前主席。<br />
<br />
李禄说，其他投资者还包括金融家科尔博格(Jerome Kohlberg)，新闻集团(News Corp.)董事、Allen &amp; Co.高管舒曼(Stanley Shuman)，以及对冲基金经理纳什(Jack Nash)。<br />
<br />
新闻集团拥有道琼斯公司(Dow Jones &amp; Co.)和《华尔街日报》。道琼斯公司为道琼斯通讯社的出版商。<br />
<br />
不过，1998年李禄担任对冲基金经理的第一年就损失惨重。他的以投资亚洲股市为主的基金受到了亚洲债务危机的严重打击，损失了19%。<br />
<br />
他说，我感觉很糟，因为人们信任我，他们知道的只是我是一个学生活动分子，他们看到的只是损失。<br />
<br />
随着亚洲危机迅速消退，他的财富反弹。1998年伊始，一场新的大牛市也随之到来。当时对冲基金行业急速发展，到1999年末，李禄的基金已经补回了损失。<br />
<br />
简&#8231;奥尔森(Jane Olson)是李禄在人权方面的一位联络人，她的丈夫罗纳德&#8231;奥尔森(Ronald Olson)是巴郡的董事，还曾是芒格帮助创建的一家洛杉矶法律事务所的早期合伙人。李禄会去奥尔森夫妇位于加州圣巴巴拉的周末度假房，2003年的感恩节，他遇到了家在附近的芒格。<br />
<br />
芒格说，李禄马上就给他留下了印象。他说，两人都对金融公司报告的收益抱有怀疑，也都不喜欢听那些废话。<br />
<br />
芒格给了李禄一部分家庭储蓄用于投资，创立了一个押注严重受挫股票的「价值」基金。<br />
<br />
李禄说，两周后他与芒格再度会面，以确保后者的听证权。2004年初，李禄创立了一个基金，自己投资400万美元，又从其他投资者那里筹集5000万美元。芒格的家族投资5000万美元，后来又投资3800万美元。李禄与芒格达成的协议中有基金将不再向新投资者开放的内容。<br />
<br />
李禄获得成功始于2002年首次投资比亚迪，当时比亚迪只是一家羽翼未丰的中国电池公司。比亚迪创始人出身贫寒，1995年用借来的30万美元开创了这家公司。<br />
<br />
比亚迪在香港证交所进行首次公开募股(IPO)后不久，李禄就投资了这家公司。（比亚迪在美国「粉单」(Pink Sheets)市场交易，最近报每股6.90美元。）<br />
<br />
李禄创立自己的基金后，再次买进比亚迪股票，最终将与芒格共同经营的1.5亿美元基金中很大一部分都投向了比亚迪，后者当时迅速发展，已经收购了一家破产的中国汽车u造商。芒格说，李禄一开始只买了一点儿，后来比亚迪股票下跌时又买入更多，这是他的特点。<br />
<br />
2008年，芒格还说服索科尔为巴郡调查比亚迪。索科尔去了中国，回到美国后，他与芒格一同劝说巴菲特加大对比亚迪的投资。9月，巴郡向比亚迪投资2.3亿美元，收购了该公司10%的股份。<br />
<br />
比亚迪的业务一直火爆。现在它已琢巳球用于手机的锂电池市场近三分之一的份额。比亚迪更宏大的计划包括电动汽车和溷合动力车方面的业务。<br />
<br />
作为中国最大的汽车厂商之一，比亚迪面临的考验在于能否实现其开发市场上能效最高的锂电池的计划，这种电池未来可能成为更强劲的动力来源。更有前景的是，锂电池有可能用于存储太阳能和风能等其他能源产生的电力。<br />
<br />
芒格说，大型锂电池将改变整个行业的状况。<br />
<br />
比亚迪是李禄重点押注的对象。他是该公司的非正式顾问，并拥有约2.5%的股权。<br />
<br />
李禄的基金向比亚迪投资的4000万美元现在价值约4亿美元。巴郡2008年投资的2.3亿美元现在价值约15亿美元。巴菲特、芒格、索科尔和李禄，还有微软创始人兼巴郡董事盖茨计划今年9月访问中国并参观比亚迪。<br />
<br />
现在李禄可以在有限制的条件下在中国旅行。但中国政府对他有何看法还不清楚。<br />
<br />
