													<h2>From Tiananmen Square to Possible Buffett Successor</h2>
						
						
																											<div id="postmessage_765618" class="t_msgfont">BUSINESS JULY 30, 2010&nbsp;&nbsp;<br />
<br />
By SUSAN PULLIAM <br />
Twenty-one years ago, Li Lu was a student leader of the Tiananmen Square protests. Now a hedge-fund manager, he is in line to become a successor to Warren Buffett at Berkshire Hathaway Inc.<br />
<br />
Mr. Li, 44 years old, has emerged as a leading candidate to run a chunk of Berkshire's $100 billion portfolio, stemming from a close friendship with Charlie Munger, Berkshire's 86-year-old vice chairman. In an interview, Mr. Munger revealed that Mr. Li was likely to become one of the top Berkshire investment officials. &quot;In my mind, it's a foregone conclusion,&quot; Mr. Munger said.<br />
<br />
The job of filling Mr. Buffett's shoes is among the most high-profile succession stories in modern corporate history. Mr. Buffett, who will turn 80 in a month, says he has no current plans to step down and will likely split his job after he leaves the company into separate CEO and investing functions. Mr. Li's emergence as a contender to oversee Berkshire investments is the first time a name has been identified to fill the investment part of Mr. Buffett's legendary role. <br />
<br />
The development illustrates that Berkshire is moving toward putting in place―possibly sooner than investors anticipated―certain aspects of its succession plan.<br />
<br />
The Chinese-American investor already has made money for Berkshire: He introduced Mr. Munger to BYD Co., a Chinese battery and auto maker, and Berkshire invested. Since 2008, Berkshire's BYD stake has surged more than six-fold, generating profit of about $1.2 billion, Mr. Buffett says. Mr. Li's hedge funds have garnered an annualized compound return of 26.4% since 1998, compared to 2.25% for the Standard &amp; Poor's 500 stock index during the same period. <br />
<br />
Mr. Li's ascent on Wall Street has been no less dramatic. He spent his childhood shuttling between foster families after his mother and father were sent to labor camps during the Cultural Revolution. After the Tiananmen Square protest, he escaped to France and came to the U.S. Investors in his hedge fund have included a group of senior U.S. business executives and the musician Sting, who calls Mr. Li &quot;hardworking and clever.&quot;<br />
<br />
Mr. Li's investing strategy represents a significant shift for Mr. Buffett: Mr. Li invests chiefly in high-technology companies in Asia. Mr. Buffett typically has ignored investments in industries he says he doesn't understand. <br />
<br />
Mr. Buffett says Berkshire's top investing job could be filled by two or more managers who would be on equal footing and divide up responsibility for managing Berkshire's $100 billion portfolio. David Sokol, chairman of Berkshire unit MidAmerican Energy Holdings, is considered top contender for CEO. Mr. Sokol, 53, joined MidAmerican in 1991 and is known for his tireless work ethic. <br />
<br />
In an interview, Mr. Buffett declines to comment directly on succession plans. But he doesn't rule out bringing in an investment manager such as Mr. Li while still at Berkshire's helm.<br />
<br />
