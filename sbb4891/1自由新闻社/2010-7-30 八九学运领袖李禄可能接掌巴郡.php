<br />
&quot;I like the idea of bringing on other investment managers while I'm still here,&quot; Mr. Buffett says. He says he doesn't preclude making a move this year, though he adds that there is no &quot;goal&quot; to bring on an additional manager that quickly either. Mr. Buffett says he envisions a team approach in which the Berkshire investment officials would be &quot;paid as a group&quot; from one pot, he says. &quot;I don't want them to compete.&quot; <br />
<br />
Mr. Li fits the bill in some important ways, Mr. Buffett says. &quot;You want someone&quot; who &quot;can think about problems that haven't yet existed before,&quot; he says. Mr. Li is a contrarian investor, loading up on BYD shares when they were beaten down. And he's a big fan of Berkshire, which may also help his cause. &quot;We don't want them unless they have special feelings about Berkshire,&quot; Mr. Buffett says. <br />
<br />
But hiring Mr. Li could be risky. His big bet on BYD is his only large-scale investing home run. Without the BYD profits, his performance as a hedge-fund manager is unremarkable. <br />
<br />
It's unclear whether he could rack up such profits if managing a large portfolio of Berkshire's.<br />
<br />
What's more, his strategy of &quot;backing up the truck,&quot; to make large investments and not wavering when the markets turn down could backfire in a prolonged bear market. Despite a 200% return in 2009, he was down 13% at the end of June this year, nearly double the 6.6% drop in the S&amp;P-500 during the period.<br />
<br />
Mr. Li declines to discuss a potential Berkshire position, saying only that he feels fortunate to be a member of the Berkshire inner circle. &quot;This is the stuff you can't conjure in dreams,&quot; he says.<br />
<br />
Mr. Li was born in 1966, the year Mao Zedong's Cultural Revolution began. When he was nine months old, he says, his father, an engineer, was sent to a coal mine to be &quot;re-educated.&quot; His mother was sent to a labor camp. Mr. Li's parents paid various families to take him in. He was shuttled from family to family for several years until moving in with an illiterate coal miner, with whom he developed a close bond, in his hometown of Tangshan. Living apart from his family as a child taught him survival skills, Mr. Li says. <br />
<br />
He was reunited with his family, including two brothers, by age 10, when a massive earthquake hit his hometown, killing an estimated 242,000 people in the area, including the coal miner and his family. His nuclear family was spared, he says, but &quot;most of the people I knew were killed.&quot;<br />
<br />
At the time, he says he had no direction and was fighting in the streets. Mr. Li says his grandmother, who was among the first women in her city to attend college, inspired him to begin reading and studying. He later attended Nanjing University, majoring in physics.<br />
<br />
In April 1989, he traveled to Tiananmen Square in Beijing to meet with students who were gathering to mourn the death of Secretary General Hu Yaobang, who was viewed as a supporter of democracy and reforms. <br />
<br />
The students protested against corruption, among other things, and Mr. Li helped organize the students and participated in a hunger strike. <br />
<br />
He and other students fled to France. Later in 1989, he traveled to the U.S. to speak at Columbia University, where human-rights activists embraced him as a hero. He spoke little English but landed an advance to write a book about his experiences. <br />
<br />
Helped by financial scholarships at Columbia, Mr. Li quickly learned English. He simultaneously earned three degrees: an economics degree, a law degree and a graduate degree in business, according to Columbia.<br />
<br />
With his student loans piling up, Mr. Li attended a lecture by Mr. Buffett at Columbia in 1993. At the time, the 1990s bull market was in full swing, and hedge funds were on the rise. Mr. Li says in China he didn't trust financial markets but hearing Mr. Buffett helped him overcome skepticism about stock investing.<br />
<br />
He began dabbling in stocks using money from his book advance. By his graduation in 1996, he had built a sizable nest egg and says he thought he could retire. Instead he took a job at securities firm Donaldson Lufkin &amp; Jenrette and then left to set up his own hedge fund. In 1997, he had set up Himalaya Partners, a hedge fund. Later he started a venture-capital fund to invest in U.S. technology companies.<br />
<br />
It was a heady time on Wall Street. The Internet boom was beginning. Investors were clamoring to find hot stocks.<br />
<br />
Through his human-rights contacts, Mr. Li quickly attracted well-heeled clients including Bob Bernstein, former chairman of Random House and founder of Human Rights Watch as well as the musician Sting. Other investors included financier Jerome Kohlberg, News Corp. director emeritus and Allen &amp; Co. executive Stanley Shuman and hedge fund manager Jack Nash, Mr. Li says. <br />
<br />
But Mr. Li bombed out in 1998, his first year as a hedge fund manager. His fund, which was invested chiefly in Asian stocks, was hammered by the Asian debt crisis, and lost 19%. <br />
<br />
&quot;I felt bad that people had trusted me,&quot; he says. &quot;All they knew was I was a student activist and all they saw was losses.&quot; <br />
<br />
His fortunes rebounded as the Asian crisis quickly faded. As 1998 began, so did a huge new bull market. By now, the hedge-fund industry was growing gangbusters, and by the end of 1999, Mr. Li's fund had regained its losses. <br />
<br />
In 2002, hedge-fund giant Julian Robertson gave Mr. Li money to invest in his fund on the condition that the fund would make bearish as well as bullish bets on companies. <br />
<br />
It wasn't a good fit. Mr. Li says he &quot;hated&quot; betting against stocks, complaining that he had to &quot;trade all the time&quot; to adjust his portfolio. (The remaining parts of the fund now are being unwound.) Mr. Robertson declined to comment on the business relationship.<br />
<br />
One of Mr. Li's human-rights contacts was Jane Olson, the wife of Ronald Olson, a Berkshire director and early partner at a Los Angeles law firm Mr. Munger helped found. Mr. Li began spending time at the Olsons' weekend home in Santa Barbara, Calif., and on Thanksgiving 2003 met Mr. Munger, whose home is nearby.<br />
<br />
Mr. Munger says Mr. Li made an immediate impression. The two shared a &quot;suspicion of reported earnings of finance companies,&quot; Mr. Munger says. &quot;We don't like the bull―.&quot;<br />
<br />
Mr. Munger gave Mr. Li some of his family's nest egg to invest to open a &quot;value&quot; fund betting on beaten-down stocks.<br />
<br />
Two weeks later, Mr. Li says he met again with Mr. Munger to make certain he had heard right. In early 2004, Mr. Li opened a fund, putting in $4 million of his own money and raising an additional $50 million from other investors. Mr. Munger's family put in $50 million, followed by another $38 million. Part of Mr. Li's agreement with Mr. Munger was that the fund would be closed to new investors.<br />
<br />
Mr. Li's big hit began in 2002 when he first invested in BYD, then a fledgling Chinese battery company. Its founder came from humble beginnings and started the company in 1995 with $300,000 of borrowed money.<br />
<br />
Mr. Li made an initial investment in BYD soon after its initial public offering on the Hong Kong stock exchange. (BYD trades in the U.S. on the Pink Sheets and was recently quoted at $6.90 a share.)<br />
<br />
When he opened the fund, he loaded up again on BYD shares, eventually investing a significant share of the $150 million fund with Mr. Munger in BYD, which already was growing quickly and had bought a bankrupt Chinese automaker. &quot;He bought a little early and more later when the stock fell, which is his nature,&quot; Mr. Munger says. <br />
<br />
In 2008, Mr. Munger persuaded Mr. Sokol to investigate BYD for Berkshire as well. Mr. Sokol went to China and when he returned, he and Mr. Munger convinced Mr. Buffett to load up on BYD. In September, Berkshire invested $230 million in BYD for a 10% stake in the company. <br />
<br />
BYD's business has been on fire. It now has close to one-third of the global market for lithium-ion batteries, used in cell phones. Its bigger plans involve the electric and hybrid-vehicle business. <br />
<br />
The test for BYD, one of the largest Chinese car makers, will be whether it can deliver on plans to develop the most effective lithium battery on the market that could become an even bigger source of power in the future. Even more promising is the potential to use the lithium battery to store power from other energy sources like solar and wind.<br />
<br />
Says Mr. Munger: &quot;The big lithium battery is a game-changer.&quot;<br />
<br />
BYD is a big roll of the dice for Mr. Li. He is an informal adviser to the company and owns about 2.5% of the company.<br />
<br />
Mr. Li's fund's $40 million investment in BYD is now worth about $400 million. Berkshire's $230 million investment in 2008 now is worth about $1.5 billion. Messrs. Buffett, Munger, Sokol, Li and Microsoft founder and Berkshire Director Bill Gates plan to visit China and BYD in September.<br />
<br />
Mr. Li is able to travel in China on a limited basis today, but he hopes to regain full travel privileges soon. It isn't clear how he is viewed by the Chinese government.<br />
<br />
Mr. Li declined to name his fund's other holdings. Despite this year's losses, the $600 million fund is up 338% since its late 2004 launch, an annualized return of around 30%, compared to less than 1% for the S&amp;P 500 index.<br />
<br />
Mr. Li told investors he took a lesson from watching the World Cup, comparing his investment style to soccer. &quot;You may very well work extremely hard and seldom score,&quot; he says. &quot;But occasionally―very occasionally―you get one or two great chances and you make decisive strikes that really matter.&quot;</div>

							
							
							
							
