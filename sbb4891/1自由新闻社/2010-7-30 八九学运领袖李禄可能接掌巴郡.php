已经加入美国籍的李路已为伯克希尔哈撒韦赚了大钱：他把芒格介绍给中国电池和汽车生产企业比亚迪，伯克希尔哈撒韦随后就投资了。巴菲特说，2008年以来，伯克希尔哈撒韦持有的比亚迪股份已增加逾五倍，获利约12亿美元。1998年以来李路的对冲基金复合收益年率为26.4%，而同期内标准普尔500指数为2.25%。<br />
<br />
李路在华尔街的晋升非常富有戏剧性：文化大革命期间他父母被送去劳改后，他的童年时代是在寄养家庭中辗转度过的。天安门事件后，他逃到法国，然后去了美国。他对冲基金里的投资者包括美国企业一些资深高管及音乐人Sting，后者认为李路“工作勤奋又聪明。”<br />
<br />
李路的投资策略与巴菲特明显不同：李路主要投资亚洲高科技企业，但巴菲特通常对他不了解的行业不投资。<br />
<br />
巴菲特表示，伯克希尔哈撒韦的核心投资工作可以由两个或两个以上经理管理，他们地位一样，为伯克希尔哈撒韦1000亿美元的投资组合负担不同的职责。索科尔（David Sokol）是公司旗下中美能源控股公司（MidAmerican Energy Holdings）的董事长，人们认为他是首席执行长的头号竞争者。今年53岁的索科尔于1991加入中美能源控股，以工作不知疲倦闻名。<br />
<br />
在采访中，巴菲特拒绝直接评论继任计划。但他并不排除在他仍在为伯克希尔哈撒韦掌舵期间，引入李路这样的投资经理。<br />
<br />
巴菲特说，我喜欢在我仍在这个岗位的时候引入其他投资经理的这个想法。他说不会排除今年作出这样的举动，但他补充说，也没有为很快补充投资经理设定“目标”。巴菲特说，他想象这是一个团队，伯克希尔哈撒韦的投资高管作为一个团队接受报酬，他说，我不希望他们竞争。<br />
<br />
巴菲特说，李路在许多重要方面适合这一职位。他说，你希望有人能够在新问题还没有发生前就未雨绸缪。李路是一个逆向投资者，在比亚迪股票遭打压下跌的时候，他买入了该股。另外他还是伯克希尔哈撒韦的忠实粉丝，这或许也可以帮助他的事业。巴菲特说，除非他们对伯克希尔哈撒韦有特殊的感情，否则我们不会雇佣他们。<br />
<br />
但聘用李路可能有风险，他对比亚迪的大量押注是他唯一一次大规模投资的全胜记录，如果没有比亚迪的获利，他作为对冲基金经理的表现则乏善可陈。<br />
<br />
如果让他管理伯克希尔哈撒韦的一个大规模投资组合，他是否能获得这样的利润，这仍是个未知数。<br />
<br />
更重要的是，他的战略是进行大笔投资，在市场下滑时也毫不动摇，这种战略可能会在漫长的熊市中产生事与愿违的效果。尽管2009年获得了200%的回报，截至今年6月底前，他管理的投资组合已经缩水13%，是同期标准普尔500指数6.6%跌幅的近两倍。<br />
<br />
李路拒绝讨论在伯克希尔哈撒韦的可能任职，只是说能成为伯克希尔哈撒韦内部圈子的一份子，他感到很幸运。他说，这是你做梦也想像不到的。<br />
<br />
李路出生于1966年文革开始的那一年。他说，他九个月大时，他当工程师的父亲被送到了一个煤矿接受“再教育”。他的母亲被送到了一个劳改所。李路的父母向很多家付钱，希望他们照顾他。有好几年，他都在好几个家庭之间辗转，直到到了家乡唐山一个文盲矿工的家里。他和这个矿工建立了深厚的感情。李路说，小小年纪就与家人分离，这教会了他生存的技能。<br />
<br />
十岁后，他与父母和两个兄弟重新团聚。当时他的家乡唐山发生了一场大地震，造成约24.2万人遇难，其中包括照顾他的矿工一家。他说，他们家在地震中幸免，不过他认识的大部分人都死了。<br />
<br />
李路说，当时他没有方向，在街上打架斗殴。他说，他的祖母激励他开始读书学习。他的祖母是她们市里第一批女大学生之一。李路后来上了南京大学，专业是物理。<br />
<br />
1989年4月，他到北京天安门广场遇到了在那里集会悼念胡耀邦的学生。胡耀邦被视为是一个民主与改革的支持者。<br />
<br />
学生们抗议腐败等问题，李路参与了学生的组织工作，并参加了绝食。<br />
<br />
他和其他学生后来逃往法国。1989年晚些时候，他到了美国，在哥伦比亚大学发表演讲。那里的人权活动人士像迎接英雄一样地迎接了他。他不太会说英语，不过却收到了一笔写书的预付款，书的内容是关于他自己经历的。<br />
<br />
在哥伦比亚大学奖学金的帮助下，李路迅速学会了英语。据哥伦比亚大学说，他成了该校第一批同时获得三个学位的学生之一：经济学、法学和商业硕士学位。<br />
<br />
在李路的学生贷款不断增加之际，1993年他听了巴菲特在哥伦比亚大学的一次讲座。当时，90年代的牛市正如火如荼，对冲基金在上升阶段。李路说，在中国，他不相信金融市场，不过听过巴菲特的讲座之后，帮助他克服了对股市投资的质疑。<br />
<br />
他开始用写书的预付款投资股市。1996年毕业前，他已经有了相当规模的储蓄，他说他觉得自己可以退休了。然而，他接受了证券公司帝杰(Donaldson Lufkin &amp; Jenrette)的一份工作，之后离职创建了自己的对冲基金。1997年，他创建了对冲基金Himalaya Partners。之后，他建立了一只风险投资基金，以便投资美国的科技公司。<br />
<br />
当时正值华尔街令人迷醉的时候。互联网热刚刚开始。投资者们吵着找热门股。<br />
<br />
通过他的人权人士的关系，李路迅速吸引了包括伯恩斯坦(Bob Bernstein)和音乐家斯汀(Sting)在内的富有客户。伯恩斯坦是兰登书屋(Random House)前董事长，也是“人权观察”前主席。李路说，其他投资者还包括金融家科尔博格(Jerome Kohlberg)，新闻集团(News Corp.)董事、Allen &amp; Co.高管舒曼(Stanley Shuman)，以及对冲基金经理纳什(Jack Nash)。<br />
<br />
新闻集团拥有道琼斯公司(Dow Jones &amp; Co.)和《华尔街日报》。道琼斯公司为道琼斯通讯社的出版商。<br />
<br />
不过，1998年李路担任对冲基金经理的第一年就损失惨重。他的以投资亚洲股市为主的基金受到了亚洲债务危机的严重打击，损失了19%。<br />
<br />
他说，我感觉很糟，因为人们信任我，他们知道的只是我是一个学生活动分子，他们看到的只是损失。<br />
<br />
随着亚洲危机迅速消退，他的财富反弹。1998年伊始，一场新的大牛市也随之到来。当时对冲基金行业急速发展，到1999年末，李路的基金已经补回了损失。<br />
<br />
2002年，对冲基金巨头朱利安&#8226;罗伯森(Julian Robertson)斥资投资李路的基金，条件是基金在做多的同时也要做空。<br />
<br />
这样的条件并不合适。李路说他痛恨做空股票，抱怨说他不得不一直进行交易，调整自己的投资组合。（该基金剩余的部分目前正在清理。）罗伯森拒绝就这一商业关系发表评论。<br />
<br />
简&#8226;奥尔森(Jane Olson)是李路在人权方面的一位联络人，她的丈夫罗纳德&#8226;奥尔森(Ronald Olson)是伯克希尔的董事，还曾是芒格帮助创建的一家洛杉矶法律事务所的早期合伙人。李路会去奥尔森夫妇位于加州圣巴巴拉的周末度假房，2003年的感恩节，他遇到了家在附近的芒格。<br />
<br />
芒格说，李路马上就给他留下了印象。他说，两人都对金融公司报告的收益抱有怀疑，也都不喜欢听那些废话。<br />
<br />
芒格给了李路一部分家庭储蓄用于投资，创立了一个押注严重受挫股票的“价值”基金。<br />
<br />
李路说，两周后他与芒格再度会面，以确保后者的听证权。2004年初，李路创立了一个基金，自己投资400万美元，又从其他投资者那里筹集5,000万美元。芒格的家族投资5,000万，后来又投资3,800万。李路与芒格达成的协议中有基金将不再向新投资者开放的内容。<br />
<br />
李路获得成功始于2002年首次投资比亚迪，当时比亚迪只是一家羽翼未丰的中国电池公司。比亚迪创始人出身贫寒，1995年用借来的30万美元开创了这家公司。<br />
<br />
比亚迪在香港证交所进行首次公开募股(IPO)后不久，李路就投资了这家公司。（比亚迪在美国“粉单”(Pink Sheets)市场交易，最近报每股6.90美元。）<br />
<br />
李路创立自己的基金后，再次买进比亚迪股票，最终将与芒格共同经营的1.5亿美元基金中很大一部分都投向了比亚迪，后者当时迅速发展，已经收购了一家破产的中国汽车制造商。芒格说，李路一开始只买了一点儿，后来比亚迪股票下跌时又买入更多，这是他的特点。<br />
<br />
2008年，芒格还说服索科尔为伯克希尔调查比亚迪。索科尔去了中国，回到美国后，他与芒格一同劝说巴菲特加大对比亚迪的投资。9月，伯克希尔向比亚迪投资2.3亿美元，收购了该公司10%的股份。<br />
<br />
比亚迪的业务一直火爆。现在它已占了全球用于手机的锂电池市场近三分之一的份额。比亚迪更宏大的计划包括电动汽车和混合动力车方面的业务。<br />
<br />
作为中国最大的汽车厂商之一，比亚迪面临的考验在于能否实现其开发市场上能效最高的锂电池的计划，这种电池未来可能成为更强劲的动力来源。更有前景的是，锂电池有可能用于存储太阳能和风能等其他能源产生的电力。<br />
<br />
芒格说，大型锂电池将改变整个行业的状况。<br />
<br />
比亚迪是李路重点押注的对象。他是该公司的非正式顾问，并拥有约2.5%的股权。<br />
<br />
李路的基金向比亚迪投资的4,000万美元现在价值约4亿美元。伯克希尔2008年投资的2.3亿美元现在价值约15亿美元。巴菲特、芒格、索科尔和李路，还有微软创始人兼伯克希尔董事比尔&#8226;盖茨(Bill Gates)计划今年9月访问中国并参观比亚迪。<br />
<br />
现在李路可以在有限制的条件下在中国旅行。但中国政府对他有何看法还不清楚。<br />
<br />
李路拒绝透露基金的其他资产。虽然今年出现亏损，但这个规模6亿美元的基金自2004年底创立以来已经增长了338%，年化回报率约30%，而标普500指数年化回报率不足1%。<br />
<br />
李路对投资者说，他从世界杯观赛经历中汲取了一项教训，他将自己的投资风格比作足球。他说，你有可能踢得非常卖力，但就是进不了球，但偶然之间──非常偶然──你得到一两个好机会，从而踢进决定性的进球。<br />
<br />
Susan Pulliam</div>

							
							
							
							
