谭：是你骚扰他们？哈哈<br />
浦：他老骚扰别人。他印了一个，一张纸，上面写着：请中共官员公布财产，下面有行小字叫“温馨小提示：请公仆别对主人动粗”。周老师（音）他们的都很好。<br />
谭：他有一些通道……<br />
浦：每周都给你送钱是吧？<br />
谭：是。有一些比如说政府方面公安方面有些意见就是通过他转移出来的。<br />
浦：没事，一切都好。在你之后成都方面基本上没人受到更严重的对待。<br />
谭：好，这就好。请你转告大家，千万不要再进来，不要再为这些事情……。这确实是，也为他们好。感谢他们做的很多事情。但是不要再为这个事情连累更多。<br />
浦：就是不要把你这事儿进来更多的人是吧？其实我也说过，如果说谭作人有罪的话，那么在六四的问题他不及我，在地震调查上艾未未不落后于他，那这个证人和辩护律师都应该留下来才对，但北京方面对这个事情毫不关心，我们这边所有的作为北京市公安局司法局所有的事情就跟没有发生过一样，成都的孤立是非常明显的，这个当官的以邻为壑，他才不关心四川发生了什么事情。所以说，我也不知道他们该怎么个做法。<br />
大家都很惦记你：肖雪慧，彭**，谢**，艾未未，艾南山，范晓，冉云飞，陈云飞，陈*，独钓翁，流沙河，黄一龙等，大家都要我向你致意。他们忘了说，没有人说让你好好改造，认罪伏法，哈哈！张**、章**老师要我专门向你致意。夏霖和我们那个助手都要我问候你，蔡**、周**，另外呢就是，十月中旬以来很多的海外媒体问你案件进展情况，我说法官说了案件敏感，但这句话不该出自法官之口，他说还没有消息，说明案子不是他审的，他无法独立行使审判权。你还对家里有什么话么？对外边？<br />
谭：对外面就是对艾未未，冉云飞，反正我就不一一说了，就是所有你……，你这儿都有名字啊，这些朋友，都给他们说，我感谢他们，希望他们自己也保重，注意安全，对家里我还是那三个字：对不起。<br />
浦：他们为你自豪。所有的朋友都为你自豪，我也很敬佩你。好好为坐牢，另外我觉得你这个如果没别的事干，研究一下看守所体制，这是个学术问题，所有的教授都没能做到，所有坐牢的人都没这个心思，那么你既然无事可做… <br />
