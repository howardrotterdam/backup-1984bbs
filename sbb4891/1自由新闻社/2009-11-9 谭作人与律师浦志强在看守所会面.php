																											<div id="postmessage_319365" class="t_msgfont">谭作人与律师浦志强的会面<br />
<strong>视频54楼</strong><br />
<br />
2009年10月26日，浦志强律师在成都温江看守所会见单独会见了谭作人，本次会见为一审后初次会见，会见时间从上午11点左右到下午1点多，约2个小时，并对会见内容进行了部分录像。<br />
<br />
以下文字是从录像片段中截取整理：<br />
……<br />
浦：我应该带个摄制组进来，但我们不想动静太大，你都知道这是对你的一种照顾是吧？<br />
谭：对，这个照顾我从一开始就感觉到了。<br />
浦：你怎么感觉到的？对别人不是这样的吗？<br />
谭：一进来后听到其他人讲嘛，其它监房打架。<br />
浦：到了过渡监室之后，就已经固定监室是吧？<br />
谭：对，我觉得这个固定监室也就是照顾性质的监室。这个是他们所长自己讲的。<br />
浦：选择这个温度不高的对吧？<br />
谭：对。<br />
浦：也是人家有意关照你是吧，就是不希望你受折腾是吧？也不希望你挨欺负是不是？<br />
谭：应该是有这个意思吧。<br />
浦：其它的监所也有打架呀什么的？<br />
谭：对。<br />
浦：那这个里面彼此也不占便宜吧，不至于说谁欺负谁。<br />
谭：基本上不会的。<br />
浦：饭能吃饱吧？<br />
谭：都吃的饱。<br />
浦：你是个老油条？<br />
谭：这里面的一个潜规则就是老犯人是有地位的，时间长的。现在我在我那个监室是排名第二。<br />
浦：时间长的就有地位对吧？<br />
谭：对。<br />
浦：第一的是干什么的？最长的那个是怎么进来的？<br />
谭：第一个实际上比我晚，但是他是处长，这个里面的管理的。<br />
浦：就是这个看守所的一个处长？<br />
谭：不是，就是犯人里边，也不能这样讲。只是说找一个人为大家服务吧。<br />
……<br />
浦：我知道。就是大家相处的都很好是吧？<br />
谭：对，相处的都比较好。<br />
浦：他们对你的这种情况怎么看呢？他是官员他能更明白你一些。<br />
谭：他们各有各的看法，就是同样是公务员，或者是国家干部，每个人看法都是不同的。<br />
浦：但这不妨碍大家相处。<br />
谭：不妨碍。<br />
浦：那很好。我跟你讲讲我了解的情况。<br />
谭：好。<br />
浦：那天的庭审呢。……官司我们接受之后呢，六月份过来就签了合同，就要求递手续，这你应该知道。但国保这边呢，是吧？你的案子涉及国家机密。<br />
谭：（笑）<br />
浦：关于……不给我文件。那个时候呢因为庆华大姐选中了我们，她就特别担心，你被谁给忽悠了，自己选律师，所以那个阶段她给你的信就反复强调她给你找过了，这个事就她说了算，说要你不要管了，这个情况你明白吧？<br />
谭：我知道。她写信讲过，她说我做足了。<br />
浦：到这个检察院阶段呢我们是不知道的，也没有人告诉家里可以请律师来，我觉得呢，可能你应该知道请的是谁。7月29号我们就鬼使神差过来一趟，因为担心你的这个程序往下走，再不过来就误事。结果去了法院，法院说已经分了案子，<br />
谭：他没有讲到这个材料到了法院多久了？<br />
浦：一天。<br />
谭：一天？就是29号？那就能计算到省县。<br />
浦：对。说林乔是审判长和法官，然后我们约好的第二天来见你拿起诉书。开庭那天（因为开庭前来过）我们请来了艾南山、艾未未和范晓作证人，提交的证据有财经杂志和南方周末以及发表范晓文章的中国地质杂志等等。然后视频资料有香港电视台的铿锵集，艾晓明剪辑的《我们的娃娃》，艾未未提供的视频资料，还有第一现场。<br />
谭：第一现场就是成都国保第一次找我找到了我家。<br />
浦：这些资料我都有，都找到了。那么对方在我们见你之后，10号见你，法院呢在10号的下午林强法官给我们打电话说，因为证据比较多，通知11号安排证据交换，本来我们是打算在见完你之后就把这些东西送到法院。因为证人出庭需要他们通知，有了这个证据交换的安排我们也就不必先去了，11号上午林乔打电话问 我们都有什么证据，我们说了有书证、视频和证人出庭，这是上午。然后要求法庭准备多媒体播放设备，在当庭放。为了防备法庭的设备不方便我还中午到苏宁电器买了一台步步高的DVD，发票开成了温江区看守所。法庭在10号下午因为证据比较多，安排跟公诉人做证据交换，因为证人出庭需要……这时候他就问我有什么证据，我们就说有书证视频，我们有多媒体播放设备。这是10号的下午。11号上午我们把证据和视频都交到了法院，（10号下午3点）然后下午五点他又打电话说要我们把证人名单送到法院，我们说这都下班了，他说明天早上9点前送到，我们等你，多晚都行。因为证人需要时间。当天晚上7点多钟，我们赶到法院，把名单给他，他拿上去，一会呢，又要我们写清楚证明对象内容和与案件的关联性。11号上午呢，把证据和视频交到法院。下午，交换完证据以后，7点钟左右。邓夏法官经请示答复我们，法院不同意证人出庭，也不同意当庭播放视频证据，理由是证人与案件没有关联性。但这个是否有关联性，需要庭审指证之后才能定。视频资料不能播放，是因为我们其他的书证足以证明视频所要反映的观点和事实，所以为节省时间提高效率也不能放。我们抗议，但是他们的不理会。交换过程中，法官和检察官更客气，不敢动粗。检察官分手的时候，跟我和夏霖说，我这也是职务性的。庭审的那天早上，回去要准备啊，看他的东西。冉云飞、陈云飞、 谢玲珑等被叫去喝茶。然后艾未未带了十个志愿者过来。他很辛苦。因为那天成都机场大面积停电，关闭了。他飞到重庆，打车来的成都。住抚琴西路附近的安逸158连锁酒店，半夜跟冉云飞，曾子后等人吃人民公园附近的老妈蹄花。三点钟被几十个警察砸门闯进去之后还打了他一拳。因为他不服，后来打完就不承认。后来把他们全都控制在那间酒店里。到庭审结束后，两点多才放他们走。但是抓了五个人。除了艾未未总共五个人。下午四五点他走的时候，还有人没放。庭审的那天的街上，那天对我们的安检，给我的医生，对我的体检比较认真。估计那天，法院应该不开你的庭了。一切都是为了你。<br />
谭：唉，这个事情是我最不愿意看到的情况。因为大家都卷进去这个事情都有不同程度的风险。<br />
浦：等会，听我说。我们律师不同意，所以在门口就有冲突。<br />
谭：法庭门口？<br />
浦：然后是不让我带相机，必须存，又有冲突。然后呢，是进门必须存手机，又有冲突。然后是不能带电脑进去，又有冲突。街上呢，大约有数百人。安检后进入法院院内和大厅的有百十人。能去旁听的，没有人。你的哥哥，从美国回来了。<br />
谭：他就是没有能进来吗？<br />
浦：是啊，他就是在那院里，长得跟你一样。你的小女儿没能进来。然后就是在人群中，有光头，黑T恤带两个小牌牌的特殊人士。对了，艾南山和范晓作为证人都到了法庭门口没有进来。一旦有人手机拍照，就会把人控制。街上和法院内部，目测大约有两百左右穿制服的警察。整个法院是被包围起来。国内媒体没有报道，但英文版的中国日报和环球时报做了报道。报道得不错，报道得比较客观。由于艾未未和我本人的因素，还有你。以及案件的特殊性。海外媒体关心很多，警方对媒体有很多骚扰。香港有个女记者黄嘉瑜，早晨被警察砸门。<br />
浦：有群众举报她的房间里有违禁品，可能是毒品，于是就严格搜查，慢慢的搜查，搜到了中午饭后，咱们的庭审结束，告诉她没找到，但是，百密一疏，艾未未一群人都是录音和摄影的高手，被搜查的香港记者也做了部分偷录，还有香港的一位电视记者躲开了搜，他把这个庭审的大街上的情况摄了影。<br />
艾未未在被砸门的时候就在屋子里放置了录音笔，整个的过程一览无余，国际上几十家媒体报道过你的案子和这个案子的庭审情况，不公开审理不让律师和被告人说话，控制证人殴打证人，栽赃和干扰记者被当成是中国妨碍新闻自由和司法独立的……很多人穿着有人印有你头像的T恤衫，上面写着还他自由。问题是艾未未手下的志愿者刘艳萍第二天没有被释放，所以艾未未带着刘晓原律师和刘艳萍的家人第二天又坐飞机返回成都，我被要求留下来帮她作证，我们8月13日到西安路交涉，又被支到金牛分局，直到半夜也没有得到负责人的答复，整个交涉的过程被警察和艾未未的助手公开对拍全程录像。9月2号左右，艾未未紧急制作了纪录片《老妈蹄花》，用MP4格式上传，现在这个片子翻译成德语，日语，英语等多种语言，流传很广，中国法律和公安人员的行事风格暴露无疑，可惜的是庭审的过程我们没有影像，资料如果有一天公布出来会相当震撼，所以你的案子已经从原来的专业性和区域性的一个影响力上升到国际事件，我认为成都方面子在你的问题上骑虎难下，我一直认为到期不判当然是违法的，但犹豫不决比一条心判你有罪还要相对好一些，比如说江苏组党的郭泉刚刚一审被判处有期徒刑十年，但刘晓波的案子到现在还没有起诉，他已经被关了一年了，前段时间又被延长了两个月，不判比错判要好。这个你相信嘛，然后就是你的家庭现在很好，生计不错，帮助也不少，孩子老婆都不错，大家都知道也都相信你在里边也不错，所以这样一个状况，奥巴马获得了诺贝尔和平奖，他下个月到中国首次访问。<br />
你的事情，美国，至少是众议院吧，400多票赞成，20几票反对，1票弃权通过的《中国的人权报告》，把你和刘晓波列为比较靠前的政治犯，要求总统和各界与中国的交涉中。相信你们会自由，记者无国界组织也认为中国有1200多名政治犯，其中包括你，我个人认为就是这样，但是现在也有人说整体气候不太好，郭泉的案件判决较重也是一个信号，我个人认为呢，你的事实和行为包括和官理都摆在这里，他到期不判说明上面举棋不定或者在寻找时机，我这次会到法院问问进展和情况，如果他们把案件退回检察院补充……，我会为你申请取保候审，但没有退回的时候，我会交涉超审限不判的违法行径。<br />
我还认为中国当局是现实主义者，你的问题的走向恐怕需要综合考虑国际关系，尤其是中美关系和中国和欧盟的关系，判你刑还是放掉你换取国际交往中的开明形象，并不会因为你态度是否转变，是否认罪而发生影响，如果你还没有重要到那个份儿上，痛哭流涕不会感动成都当局，如果到了那个程度，中央的决策不会顾及、不会考虑成都李春城的面子，再有高智晟当年被判缓刑，胡佳被人家耍，这个你知道吧，他认罪没给他缓刑，判他三年半，正好接着坐。<br />
