木樨地大屠杀――纪念陆肆运动二十周年<br />
　<br />
高光俊<br />
<br />
木樨地，我一生中永远难忘的地方。我去过中国和世界许多地方，但没有一个地方能像北京木樨地一样深深的刻在我们心中。从1983年8月我大学毕业分配到中国人民公安大学（原名中共政法干校），到1991年10月26日我被捕，我在木樨地南里住了8年多。在木樨地的8年中，我徜徉陶醉在北京中国古老灿烂的文明中，深感其博大，精深；在木樨地的8年中，我从精神上完全的战胜了中共，彻底的蔑视它，在木樨地的8年中，我坚定了一个信念：只有自由民主的中国才是强大的中国，才是中国和全世界人民的福音。在北京木樨地的8年中，我无数次在木樨地的护城河两岸散步，到八一湖游泳，我所住的小院距木樨地桥只有二，三百米左右。<br />
<br />
木樨地，让我更难忘的是1989年6月3日晚上8点到6月4日11点十几个小时在那里所发生的一切！<br />
<br />
除了6月4日凌晨的几个小时之外，我一直在木樨地桥头周围。我亲身参加了北京市民可歌可泣，赤手空拳与中共军队抗争的过程，也亲也目睹了中共军人开枪屠杀民众的血腥场面。二十年来，无论我到哪里，我都永远不会忘记木樨地。<br />
<br />
陆肆学运来的如此宏大，出乎我的意料之外。尽管我已经立志推翻中共，不相信群众的示威能让中共下台，但我也不能置身于这场伟大的运动之外。于是我尽我所能，鼓动警察上街游行，支持学生运动。<br />
<br />
由于那些日子连续的紧张，焦急，身心很累，6月3日晚饭后，我和女友决定到八一湖游泳。7点左右，当我们骑自行车经过木樨地桥时，长安街上气氛很紧张，道路两旁的隔离墩被搬到路中央，一些人不时地沿长安街往西边去，说是军队要进城了，他们去挡住军队，保护学生。尽管那几天不断传出军队要进城镇压学生，但是没有人肯定军队到底哪一天要动手。<br />
<br />
我刚游了一会儿，心中觉得不踏实，赶紧和女友骑车返回木樨地桥。这时军队已经推进到公主坟。成千上万的市民、学生赤手空拳，堵在街上，企图阻止军队前进。我很快就和女友失散了，也顾不了自行车，冲到最前面。此时我离最前面的军人只有100米左右。大约有三十多位军人紧紧地围在一辆打炮车车周围，他们头戴钢盔，每人手中拿着一根一米左右的白色木棍，在夜幕中很显眼。他们手中没拿枪，看来是突击队。后面还有看不到尽头的军车，还有手持各种枪支的军人。堵在军队最前面的大约有上千人，呈半月形，将军队挡住，双方僵持着。<br />
<br />
此时夜幕完全降临，越来越多的中共军队从西边过来，气氛紧张。我身边有人喊了一声：“准备砖头，当兵的要动手了！”人们马上从四周找砖头，当作自己的武器。果然，军队中那些突击队员手持木棍突然冲向人群，见人就打，人群急忙后退，同时用石头还击。几十个军人显然不及上千民众的攻击，很快又退回到打炮车车周围，紧紧缩在一起。我身边的一位小伙子反应慢了一点，被军队一棍打中手臂，他一边疼的龇牙咧嘴，一边还幽默地对我说：“丫挺的，过早失去了战斗力！”<br />
<br />
军队突击队也在地上到处找石头，每人身上装得满满的，然后突然一起冲向我们，他们不用木棍，而用石头砸我们。我们先是后退一点，然后我们用石头铺天盖地地还击。军人们只好退回到打炮车周围，这样反反复复，军队前进，我们还击。由于我们人群集中，又没有头盔，军队每次攻击，几乎每一块砖头都会砸到一个人。尽管我们人多，但军人有保护，他们每次撤回时，都紧紧地缩在打炮车周围，几乎没有受伤。<br />
<br />
我身边不时有人被军队的石头砸中，有人痛得叫，但是更多的人自己受伤流血也没有叫唤，甚至不知道。此时我环视了一下自己周围，发现来自城市不同岗位，不同地方的市民自觉地有效分工。和我站在前面的几百人几乎都是年轻人。我们是抛石头的战斗者，我们身后则有很多妇女，姑娘，他们负责找武器“石头”。我们每个战斗者身后都有一两个人准备着石头供我们使用。一个姑娘用白色连衣裙兜着石头，很显眼。<br />
<br />
就在我们和军队战斗间隙，一位学生模样的青年不知从哪里找来一块白布，系在一根小树枝上，手拿白布，突然从我身边冲出人群，边走边向军队喊“别打了，我来谈判！”我大叫他回来，他不听，想拉住他，也来不及。当他走到离军队只有十几米时，一群军人突然手持木棍冲向他，对他一阵乱打，那学生马上被打倒在地。人群一阵呐喊，我们立即用石头还击。军队后撤，我们将那位学生拖回来，但他被打得满头是血，已经不能说话了，他被人运走，我不知道他是死是活。印象最深的是，他被拖走时，手里还紧紧地抓着那白布作的白旗，他要做和平的使者。<br />
<br />
在我们的阻挡之下，军队推进的速度很慢。大约到晚上十点左右，军队才推进到木樨地桥西边一二百米的地方。一位五十多岁的妇女说：“我们不能再退了，后面就是木樨地桥，如果军队过了桥，就会直奔天安门，学生就会遭殃。”但是此时更多的军队聚集到军车的前面，他们有的持枪，有的持棍，看样子要再次攻击我们。<br />
<br />
突然，数百名军人向我们冲过来，我们急忙后退，但军队冲得很快，他们从马路中央突破，我和一百多人来不及后退，被围在马路南铁道部大院这边，我们的武器石头没有了，也不管用，更多的军人用棍棒袭击我们，人们纷纷翻上院墙，跳进铁道部大院。青年人没有问题。可以翻墙，可是年纪稍大的，还有女人就惨了。他们很难翻上一人多高的院墙。我从后面帮了几个人翻上院墙，当我自己翻上去，回头后望时，看见到处都是军队用棍棒殴打市民，有些市民已经倒在地上，军队还在围攻。此时，一位姑娘拼命想要翻上院墙，我在墙上用手连拽带拉，可是她就是没有力气爬上来，我也使不上劲。突然，四五个军人冲上来，一名军人对着我的手打过来，我一松手，整个人从墙上摔到铁道部大院里。我听到院墙外那位姑娘的哭喊声，还有其他人的叫骂声，夹杂着军人打人的声音。我们这一群人愤怒的隔墙咒骂他们，这显然没有用。我倒下去的地方，像是铁道部一个自行车停车棚。我找了一辆自行车，靠在墙上，站在自行车上向街上望去，只见满街都是军人，军车，但是他们都被堵在木樨地桥西边。有人和我一样看到这情景，说去木樨地桥。这样我们这一群人从铁道部，绕到中联部大院。<br />
<br />
我现在记不清是怎样通过中联部大院的。中联部是一个神秘的戒备森严的地方，平时都有武警守备，可是那天晚上那些武警完全站在我们一边，任由我们进出。<br />
<br />
中联部大门座西向东，对着护城河，木樨地桥就在旁边。中联部门前有一片小树林，紧靠着木樨地桥。我和一群人就藏在这片小树林里面。<br />
<br />
市民将三辆公交车并排横在木樨地桥东头，试图挡住中共的军队。数千人站在公交车后面与军队对峙。军队开始用打炮车推公交车，不成功，于是往后退，开足马力猛撞。此时，数千人在车侧面用肉身顶住公交车，几个人站在自行车上。当打炮车快速冲向公交车的时候，他们一齐喊“一，二，三”，众人一齐用力顶住汽车。撞击声，打炮车轰鸣声，在黑暗中发出巨响，公交车猛烈晃动，但却没有倒下。四周市民欢呼，而我们被挡在桥西侧的人则不停的用石头砸向军队，向桥东的市民呐喊助威，军人也冲出来攻击我们。打炮车一次又一次的冲向公交车，人们一次又一次的用身体顶住公交车，每次都是打炮车失败退了回来。我看到第一排的汽车已经快被打炮车撞破，但是后面的市民还是不退让，死死的顶住汽车。我无法想象用肉身顶住打炮车的攻击所承受的撞击力有多大。我在桥西，看不到第一排紧贴汽车的人是如何顶住汽车的，但是我可以看见他们后面是人顶人，形成人墙。<br />
<br />
军人刚开始是想用打炮车慢慢撞倒，撞烂公交车，但是人们用身体挡住，他们撞不倒。就算撞烂前排的汽车，市民又会推来一辆组成新的一排。接着军队发射催泪瓦斯，企图趁机再撞。但是人群不怕，尽管被瓦斯攻击，还是有足够的人顶住汽车。军队短暂的停止了攻击，此时我身边的一个人说：“不好，我当过兵，这是要采取大行动了。”果然，军队突然向桥东人群发射很多瓦斯，数辆打炮车一起开足马力，轰鸣的冲向汽车，而且响起枪声。这时我第一次听到枪声，我听得出这是真的枪声。人群开始四处逃跑。刚开始我们还以为这是空枪，没有弹头。但是很快我们就听到子弹划过夜空的声音，还有打在树上，石头上冒出的火花。大部分人纷纷逃走，我和一小部分人每人找一棵树，藏在树后面。木樨地桥上的汽车已经被军人的打炮车从中间撞到两边，军队像潮水一样通过木樨地桥向东推进，他们边走边不断的向两边开枪，冲锋枪连发的声音让人仿佛置身于战场。我附近的人们发现军人是真的开枪，纷纷咒骂共产党。我和几个躲在树后的年轻人不是用石头像街上的军队砸去，每次都会招来几枪。当时我自己明知这样的抵抗是没有用的，但是大家似乎本能地反抗着。<br />
<br />
我前面的一个青年人，和我一样不停的用石头砸向军队，一边砸一边喊“打倒共产党，打倒法西斯！”然后就躲在我左前一米左右的树后，但他靠在树后半天年没有动静，我爬过去从后面推了他一下，他就一声不响的倒下了，他前额中了枪，已经死了。我把他拖到中联部门口。此时几个人用平板车推着一个腹部中枪的老太婆，大家要求中联部的卫生所救人，他们同意了，将人送进去了。<br />
<br />
此时，枪声此起彼伏，如同电影上的战场。那片小林子太靠近大街，我不能回去。说也奇怪，虽然看到不少人中枪而死，但自己并没有太害怕，而且我发觉周围的人也一样，都是咒骂共产党，说日本人进北京城也没有这样开枪杀人。<br />
<br />
于是我沿着护城河向南，从公安大学前面的小桥到北边，想到22楼和24号楼之间看看军队的暴行。只见人群纷纷往后逃，不断有人中枪倒地。军人有时三五一伙，冲进巷内，扫射人群。后来我才知道，公安大学校长的李姓司机19岁的弟弟就在这里被打死了。<br />
<br />
大约1点左右，我退到到护城河南边，在公安大学门口，门卫突然大叫：“高光俊，你怎么了？你浑身是血！”在门口路灯下，我才发现我自己浑身是血，但不是我的，我没有中枪，也没有受伤，我不知道是谁的血，但肯定不只是一个人的。我突然感到害怕，我无法接受这个事实，中共向手无寸铁的市民，学生开枪。我想中共如果像我在木樨地看到的那样屠杀人民，整个北京该要死多少人！<br />
<br />
我回到木樨地南里我的宿舍，没有开灯，呆呆的坐在黑暗的房间里，默默地流泪。我不得不再次接受这个观点：中国共产党不可能和平地接受任何变革。<br />
<br />
我在痛苦，悲哀，疲惫中想睡一会儿，但是怎么也睡不着。我想得最多的是天安门广场的学生，难道中共会像在木樨地那样屠杀广场的学生么？<br />
<br />
迷迷糊糊中天已发亮。我赶紧换了一身干净衣服，冲出院子，再次来到木樨地桥。此时街上一片狼藉，到处是石头，被打炮车压烂的汽车，被撞烂的汽车还在燃烧，不时会看到地上一片一片的血迹。经过昨晚的大屠杀，市民已经成惊弓之鸟，一有枪声就奔跑。胆大的人又慢慢开始聚集在街上。人们愤怒的讲述昨夜自己看到的情形，还有人到处捡地上的弹壳，证明中共军队开枪杀人。<br />
<br />
中共的屠杀确实镇住了一些人，但也激怒了很多人。当街上传言说西边又有军队开进来了，人们不约而同地又将汽车，水泥墩，推到木樨地桥中央，想再次挡住军队。<br />
<br />
大约早晨7点左右，西边果然又开来大批军队，人们纷纷退到路两边，远远的看着军队，不敢靠近。人们认为昨晚杀人的是27军，而现在来的是38军。后来证明第二天早上到达木樨地桥的是28军，而杀人的38军昨夜全部都过去了。<br />
<br />
新来的军队在木樨地桥西边停了下来，他们可能是被眼前血腥的场面惊呆了。因为新来的军队似乎很友善，胆大的人便慢慢靠近他们，我也靠近去，不少人向他们诉说昨晚军队的暴行，谴责他们镇压市民，军人都沉默不语。一位上尉军官坐在吉普车上反复说：“我们是刚来的，我们没有向市民开过一枪！”最后他被逼急了，掏出手枪，拉开枪膛说：“你们看，我们的枪没有子弹！”他又命令几个士兵拉了枪膛，果然都没有子弹。他们要市民拿出证据证明军人开枪杀人。很快有几个年轻人奔到离木樨地桥不远的复兴医院，拿来一大堆血衣，气愤地扔在这群人面前。此时没有人说话，数千双眼睛愤怒，仇恨的盯着这些军队，军人们互相看着，低下头，那位上尉军官默默地打开车门，跳下来，也不管他的士兵，往西走去。跟着，有士兵将枪扔进护城河，（我后来看到政府几个月以后还在那里捞枪），跟着往西走，有的边走边说：“人民军队不能杀人民！”有人把帽徽，领章也撕掉。接着后面大约有五，六十辆的军人都从吉普车，装甲车，打炮车车，卡车等各种军车中出来，弃车往西边走去。人们鼓掌欢呼，高喊“三十八军万岁”（我们当时还是以为他们是三十八军）。我跑了几步，追上一位军官，大喊道：“往东走，打进中南海！”那位军官停下来，看了我几秒钟，还是往西走了。军队撤走了，在木樨地桥头往西几百米之间，留下了五六十辆各种各样的军车。刚开始我们不知道干什么，很快人们爬进各种车里，寻找共军屠杀市民、学生的证据。有人搜出军队的宣传手册，高声诵读；有人搜出各种武器：手枪，步枪，冲锋枪，催泪瓦斯，子弹……我爬进路南边的一辆装甲车，突然发现一名不满二十岁的士兵，浑身发抖躲在车里，满脸惊恐，不知道他为什么没有西撤。有的人愤怒大喊，“打死他，他不撤，他还想杀我们！”人们将他拖出来，拳打脚踢，此时有三个学生，手里拿着学生证，大叫“别打了，别打了，他已经投降了，我们是北工大的学生，把他交给我们！”大家都住手，让三个学生把那个士兵带走。又是学生，在这个时候，他们还在保护那些士兵。当时，北京的大学生是市民心目中的英雄，学生的话，大家都自动服从。几分钟后，另一件事情也证明大家对学生的尊重，三个市民从一辆装甲车中找到一把手枪，相互争抢，突然一个学生亮出学生证，三个人就把枪交给那位学生。我当时也想要那把枪，我想那些学生根本不懂枪，拿着也没有用。但是他很快就消失在人群中。（我捡到三瓶催泪瓦斯，两把匕首，连同我的那身血衣，我想保留作为中共杀人的证据，但因为我后来被捕，都没有了）<br />
<br />
有位中年妇女在那里痛哭，说他的女儿昨夜在木樨地桥东北边被打死，有人愤怒的点燃一辆吉普车，更多的人大喊：“烧！”接着更多的军车被点燃。突然，一位三十多岁的男子跳进一辆打炮车车，开动打炮车车，在桥西来回奔跑。有人大喊：“开到天安门去，开到天安门去！”<br />
<br />
此时的木樨地如同战场一般，几十辆军车在燃烧，浓烟滚滚，覆盖桥头，直升机不时低空掠过，引起人们的阵阵恐慌，愤怒的民众开动没有燃烧的军车，不知想干什么。痛苦，咒骂，叫喊声到处都有，还有人发呆了，似乎还没有从大屠杀中清醒过来。<br />
<br />
突然有人大叫，西边又有军队过来！而且我们还听到枪声。人们四处逃跑，那个正在开着打炮车的人停下来，从车上跳下来，我喊道，“把它烧了！”他很快就从燃烧的车上取了一把火，扔进打炮车内，然后我们都逃走了。此后，不断有军队迅速穿过木樨地桥，军队也控制了街头，人们不敢再靠近桥头。以后那里有士兵轮流站岗。我记不清那些士兵是什么时候从桥头撤走的，只记得在炎热的夏天，那些士兵穿着笨重的大头鞋，戴着头盔，端着枪，警惕的看着每一个过往的人。<br />
<br />
多年后，我才知道木樨地是中共屠杀市民最残酷，最血腥的地方。但是究竟有多少人倒在木樨地的桥四周，恐怕只有中共最清楚。二十年来，回忆起当时那十几个小时发生的事情，我有几个感想。<br />
<br />
首先，我对中国人以及中国文化有了更深的了解，过去我们总认为中国人麻木，自私（现在还有不少人这么认为），但是那个晚上北京市民的行为真是惊天地，泣鬼神。我们现在留下王维林置身挡住打炮车的画面（王维林挡打炮车可能发生在6月5日甚至更晚，那时大屠杀已经过去，军人不再敢面对世界用打炮车车压人，所以才会有那队打炮车被王维林挡住的画面），可惜我们没有留下那数千人在木樨地桥头用肉身顶住打炮车的猛烈撞击的镜头，还有用石头与军队战斗的情景。中国人民不乏勇敢，不惧死，团结一起，真正用血肉筑成捍卫民主自由的长城。<br />
<br />
其次，我发觉中共军队当时几乎崩溃。常驻北京的军警，几乎全部倒向市民，学生。他们不但不镇压学生，反而支持学生。即使是外地进京的军队态度已完全不一样。6月4日凌晨到达木樨地的军队都撤退了。就算是6月3日晚上杀进北京的三十八军，我相信只是少数人在开枪，更多的人是对天空开枪，否则恐怕我自己也活不到今天。<br />
<br />
最后，我有一个感想：为什么当时中共高层支持，同情学运的赵紫阳一派不走进军队，市民和学生中，组成一个新的联盟？赵和他的同僚宁可下台被审判，也没有像叶利铁那样站上打炮车，让军队倒戈。我敢肯定，6月4日上午，如果赵在木樨地桥号召军队保护学生，那些军人肯定会跟他走。89年底，当罗马尼亚发生政变时，我在公安大学院内贴出“万岁，勇敢的罗马尼亚人民；起来，消灭中国的齐奥塞西库！”但是此时北京处在中共镇压的恐怖中，陆肆高潮已过，中共党内改革派已被中共肃清，我们没有机会了！<br />
<br />
木樨地桥，我将尽二十年没有回到那年让我难忘的地方！我有一个梦，有一天在木樨地桥建立一个纪念碑，永远纪念为中国民主自由而献身的市民和学生。<br />
<br />
长眠吧，倒在木樨地周围的勇士们，你们永远活在中国人民心中。<br />
<br />
二零零九年五月十七日<br />
于纽约</div>

							
							
							
							
