																											<div id="postmessage_763332" class="t_msgfont">《山楂树之恋》看哭审片领导&nbsp; &nbsp;女主角像山口百惠<br />
<br />
2010年07月28日05:25&nbsp;&nbsp;华商报<br />
<br />
[导读]7月26日，张艺谋、张伟平和广电总局审片领导们一起看过《山楂树之恋》，张伟平称这第一批观众全被打动，“我和审片的领导全落泪了，大家都很感动。”<br />
<br />
<img src="http://img1.gtimg.com/cd/pics/hv1/0/160/581/37820325.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
《山楂树》剧照<br />
<br />
华商报7月28日报道 今年下半年大银幕注定泪雨纷飞，《唐山大地震》的眼泪还未“擦干”，张艺谋的《山楂树之恋》26日晚又把审片领导看哭了。该片已定下在9月16日上映。昨日下午，制片人张伟平接受了记者的专访，他透露，看片的领导们都觉得女主角像山口百惠。张伟平称，虽然女主角“静秋”的扮演者周冬雨因拍戏未参加高考，但他今后将支持对方考电影学院。 <br />
<br />
档期9月上映是为《金陵十三钗》让路 <br />
<br />
9月16日将上映的《山楂树之恋》，离今年4月16日宣布开机，刚好五个月的时间，从拍片到上映，张艺谋又创造了一种速度。而之前，敲定9月份公映的影片已经有《狄仁杰》、《精武风云》、《剑雨》等商业大片。而之前《山楂树之恋》被大家认定是贺岁档上映的影片，与陈凯歌的《赵氏孤儿》、冯小刚的《非诚勿扰2》挤在同一档期PK。 <br />
<br />
对此，张伟平说：“前一阵大家都说我们的电影将在贺岁档上映，我们没有回应。9月份的这个档期是《山楂树之恋》开拍时就想好了的。因为我们明年初就要开拍《金陵十三钗》，为这个大制作我们准备了四年多。我们不可能把《山楂树之恋》放在贺岁档。”对于9月份大银幕上有不少对手，张伟平显得胸有成竹，“有竞争绝对是好事，不就是那些港台商业片吗？我们是给观众换口味来的。” <br />
<br />
看片《山楂树之恋》把审片领导看哭了 <br />
<br />
在公布档期的前一天，也就是7月26日，张艺谋、张伟平才和广电总局审片领导们一起看片，对于影片的反应，张伟平称这第一批观众全被打动，“我和审片的领导全落泪了，大家都很感动。”而对于女主角静秋的评价，张伟平告诉记者：“他们都觉得我们挑的这个女演员很像山口百惠，只不过比山口百惠要清瘦一些。我觉得还真挺像。”张伟平说：“静秋是艺谋自己定的，我之前都没见到过。我看完片子，觉得艺谋的眼光是对的，静秋的表现非常出色。而以前的媒体偷拍到的女主角，画面都不是很真实，在银幕上一看，效果完全不一样。” <br />
<br />
因为《唐山大地震》刚刚让观众“以泪洗面”，如今《山楂树之恋》再来，怕不怕观众很难在短时间内再次被感动？对此张伟平说：“《山楂树之恋》与《唐山大地震》题材不一样，那是灾难片，我这是爱情片，两个不同的题材，两个不同的故事。”记者问到《山楂树之恋》与张艺谋之前的艺术片《我的父亲母亲》，有什么样的突破和不一样，张伟平说：“我个人觉得这是张艺谋文艺片的新飞跃，也可以说是爱情题材电影的一次突破。跟艺谋以前的电影是不一样的，而影片究竟怎么不一样，这个要你自己去感受。” <br />
<br />
演员将支持女主角考电影学院表演系 <br />
<br />
《山楂树之恋》中除老戏骨李雪健、奚美娟等外，男女主角都是新人。对于演员的表现，张伟平说：“不管是这些老演员还是新人，都非常满意。”他说，现在男主角窦骁和女主角周冬雨都签约了新画面影业公司。 <br />
<br />
而就在前两天，诸多媒体都报道说，影片女主角周冬雨为了《山楂树之恋》，放弃了高考。对于会不会给小演员一些补偿，张伟平也表示，“有《山楂树之恋》这部电影，下一步她可以凭此去考电影学院的表演系，我们会支持她的。”</div>

							
							
							
							
