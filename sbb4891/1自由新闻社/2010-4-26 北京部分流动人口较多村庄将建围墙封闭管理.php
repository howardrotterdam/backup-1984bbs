													<h2>北京试点对流动人口较多村庄建围墙封闭管理</h2>
						
						
																											<div id="postmessage_585128" class="t_msgfont">简介：目前，大兴区有92个自然村出现“倒挂”情况，以老三余村为例，该村常住人口660人，而流动人口数量达到6000人左右。警方在工作中发现，这些村庄出现警情高发，环境和治安秩序差等情况，已经形成治安工作的瓶颈。<br />
<br />
<br />
<img src="http://i1.sinaimg.cn/dy/c/2010-04-26/U4166P1T1D20150401F21DT20100426023906.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
这些社区早6点开放，晚11点后关闭村门，无特殊情况不得出入。京华时报记者 董世彪 穆奕摄影报道<br />
<br />
<br />
<img src="http://i3.sinaimg.cn/dy/c/2010-04-26/U4166P1T1D20150401F23DT20100426023906.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
昨日，大兴区举行千人专职巡逻队社会面巡逻防控启动仪式。新京报 通讯员 吕品璋 摄<br />
<br />
<br />
据新华社电 北京警方25日在大兴区老三余村召开现场会，试点推行城乡接合部的流动人口“倒挂”村的封闭管理模式。<br />
<br />
<strong>村庄实行社区管理模式</strong><br />
<br />
　　北京市公安局大兴分局副局长左宝栓告诉记者，所谓“倒挂”是指流动人口数量超过常住人口数量。目前，大兴区有92个自然村出现“倒挂”情况，以老三余村为例，该村常住人口660人，而流动人口数量达到6000人左右。<br />
<br />
　　警方在工作中发现，这些村庄出现警情高发，环境和治安秩序差等情况，已经形成治安工作的瓶颈。<br />
<br />
　　按照封闭式的管理模式，警方联合政府部门为这些村落建围墙、安街门、设岗亭，并封闭一些不常用的路口，对村内的人员和车辆实行持证出入的措施，将原本开放的自然村经过人为科学合理改造，上升为封闭式的社区管理模式。<br />
<br />
　　　　<strong>实现网上巡逻</strong><br />
<br />
　　同时，警方在每个村庄建立村庄综治中心，将社区警务站、流动人口管理服务站、巡防工作站整合。<br />
<br />
　　同时，在每个村庄综治中心设立“民调室”，由社区民警和村委会干部共同进行村内矛盾纠纷调处工作，实现“小事不出村，大事不出镇，矛盾不上交”。<br />
<br />
　　在每个村庄，警方严格按照实有人口2.5%。，流动人口5%。的比例配备巡防队和流动人口管理员，在社区民警带领下进行村庄内部的治安防范工作。<br />
<br />
　　　<strong>统一安装监控探头</strong><br />
<br />
　　此外，根据各村街巷胡同的分布、数量等情况，评估每个村庄监控探头的需求量，确定需要安装的点位，并统一安装。警方在镇、村分别建立监控平台，实行24小时专人值守，实现村庄网上巡逻。<br />
<br />
　　据了解，自今年3月以来，率先进行试点的金星派出所辖区村庄110警情同比下降45%，发案同比下降10%。警方表示，今年内将先期在大兴区92个村庄推行封闭式管理模式。<br />
<br />
　　　<strong>■ 举措<br />
<br />
　　　　大兴千人巡防配合“封村”<br />
<br />
　　　　已招录专职巡逻队员近800人</strong><br />
<br />
　　昨日，大兴区举行了千人专职巡逻队社会面巡逻防控启动仪式。第一批250名专职巡逻队员已完成岗前培训并分配到派出所开展工作。今后，这批专职巡逻队员，将参与警方的社会巡逻防控。目前，社会巡逻队员已招录近800人。<br />
<br />
　　大兴警方介绍，大兴区六环路以北城乡接合部地区五个镇流动人口规模大，形成多个流动人口与常住人口比例倒挂村，这些村庄警情高发，环境和治安秩序差，不安全隐患多。对此，区委、区政府拨专项资金5000余万元，大力支持大兴公安分局打造一支职业化、规范化的千人专职巡逻队。<br />
<br />
　　据介绍，选拔出的巡逻队员，将参加社会面巡逻防控。目前，已招录巡逻队员近800人。<br />
