													<h2>25对沪宁直达列车停开 铁路局否认旅客&quot;被高铁&quot;</h2>
						
						
																											<div id="postmessage_708927" class="t_msgfont">核心提示：沪宁高铁7月1日将开通运行。南京铁路部门6月28日发布信息称，原南京到上海D5401次至D5450次的25对动车将随之全部停开。上海铁路局方面28日接受记者采访时，否认这是让旅客“被高铁”。<br />
<br />
新民网6月28日报道 沪宁高铁7月1日将开通运行。南京铁路部门6月28日发布信息称，原南京到上海D5401次至D5450次的25对动车将随之全部停开。上海铁路局方面今日接受新民网记者采访时，否认这是让旅客“被高铁”。<br />
<br />
据南京火车站官方网站信息，原来南京到上海的D5401-D5450次共25对列车7月1日后将全部停开，届时，经停南京且仍在普速线运行的“D”字头动车将只剩下13对，其中11对经停上海。<br />
<br />
此前上海铁路局公布的方案则显示，7月1日后，将有22对D字头长途列车在沪宁高铁线上开行，其中先期开行12对，均经停南京，另有10对待条件成熟后开行。<br />
<br />
据此推算，包括新开的沪宁高铁和原有普速线在内，7月1日后上海至南京间“先期”将有23对动车，“待条件成熟后”将有33对动车;而截至今日14时，根据上海铁路局网站提供的列车时刻表显示，上海至南京目前共有52对动车开行。<br />
<br />
这样的班次减少，很多网友质疑铁路方面是通过减少普通动车数量，将客流硬性“赶往”高铁。对此，上海铁路局有关人士今日向记者明确表态，“不存在这一可能”。<br />
<br />
他表示，7月1日后，既有的T字头、K字头列车和普通列车将全部保留，22对既有D字头长途列车转到高铁线上，同时还计划开行98对G字头列车，这样乘客就有了5种选择，“被高铁”之说不成立。<br />
<br />
<br />
据了解，去年12月26日武广高铁开通运营后，同样有减少既有普速线动车的举措，也曾遭受旅客“被高铁”的质疑。铁道部新闻发言人王勇平当时亦表示，“普速列车停运，目的是进一步优化现有列车运行方案”，“在运输淡季，运力并不十分紧张的情况下，一部分中高端乘客被分流到高速铁路上，乘客乘坐普速列车不会受到较大影响。”<br />
