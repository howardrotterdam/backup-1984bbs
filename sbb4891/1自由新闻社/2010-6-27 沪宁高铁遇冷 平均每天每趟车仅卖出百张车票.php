																											<div id="postmessage_706002" class="t_msgfont">新华报业：如何看沪宁高铁的速度和票价？<br />
您当前的位置 ：新闻 &gt; 新华网眼 &gt; 原创评论 &gt; 时政 <br />
<br />
如何看沪宁高铁的速度和票价？<br />
新华报业网　　2010-06-26 12:23:18　 <br />
<br />
6月24日，铁路部门公布了沪宁高铁的运行方案和票价，引起了众多网友的关注。作为长期关注沪宁高铁的较为资深的铁路迷，我注意到一些网友认为价格虽然贵了一点，但是节省了时间，提供了便利，可以接受。也有一些网友反映票价高了一些，列车的运行时间长了一些。我在此谈一些个人的看法，供各位网友参考。<br />
<br />
&nbsp; &nbsp; 如何看待沪宁高铁的运行时间？<br />
<br />
&nbsp; &nbsp; 一些网友说，沪宁高速铁路上海、南京两地间最快时间为73分钟，比原来既有线的运行时间压缩了1个小时，速度很快，但是很多列车运行时间接近两个小时，比原来的动车组压缩时间并不多，多付了钱似乎不值得。<br />
<br />
&nbsp; &nbsp; 据我了解，沪宁高铁运行最高时速350公里，这是一个技术标准，是指沪宁城际铁路运行的最高时速可以达到这个标准，但并不是全线的运行速度都可以达350公里，不能简单的以300除以350得出两地间的运行时间。<br />
<br />
&nbsp; &nbsp; 作为铁路迷都知道，铁路线路的选线走向是受很多因素制约的。以沪宁高铁为例，该铁路穿越城镇乡村，将江南6大城市连接在一起，在走向选择上，既要考虑到各城市发展的需要，又要尽量避免大的动拆迁，还要克服河流、软土、文物保护等影响，选线非常困难。如对高铁镇江站的设置，原先的方案是从城市外围直线穿越，这样速度可以不受影响。但根据镇江市的发展规划和要求，最终把城际站定位在既有沪宁铁路镇江站南侧，与既有车站连为一体，这样就符合了镇江市城市发展规划的需要，方便了广大旅客乘坐，但客观上造成了沪宁高铁进入镇江站前需要拐一个很大的弯道，列车通过时速最高只能135公里。也就是说，高铁列车开到这里就要减速，而且要减到时速不超过135公里，这就大大影响了列车运行速度。我还听说，沪宁高铁进入苏州由于要避开虎丘古塔，出于文物保护的需要，线路也绕了一个很大的弯，也要限速运行。像这样的限制点，全线有很多处，造成列车运行速度不断上下波动。此外，沪宁高铁是一条城际铁路，沿线有21个车站，还要顺应各地群众出行的需求，考虑各站的停靠和沿线城镇旅客的出行，办理上下客业务，每个站的启动、停车附加时分各需要3分钟，再加上办客时间，每停一个车站，至少需要有8分钟的损耗时间。所以，以一班列车办理沿线6个站停靠算，就需要48分钟时间，加上基本运行时间，全程运行时间就达到了2个小时。为什么不能少停一些车站？因为要综合考虑沿线21个车站的停靠，沿线老百姓都有出行的要求，如果不停，沿线的老百姓不答应。对于铁路而言，自然是速度越快越好，时间越短越好。但是对于沪宁高铁，则面临着拉动城乡一体化发展的命题，方方面面的因素特别是沿线城镇旅客的出行，是必须要考虑的一个问题。<br />
<br />
&nbsp; &nbsp; 一些网友认为，部分高铁列车压缩的运行时间并不多，但却要付出更高的成本，不值得。我认为，这个问题应该综合考虑。任何新生事物的出现要看它对整个社会发展的促进作用，而城际高铁的价值就在于它对沿线城镇一体化发展的促进作用，在于对长三角率先发展、一体化发展的促进作用。在它的发展进程中，总有一部分人受益，一部分人的利益短时受到影响，关键看它对整个社会经济发展所起的促进作用如何。以日本新干线为例，在上世纪60年代开通运行初期，也面临着票价、速度等重重压力，但是在今天，已经很少有人对新干线铁路的修建运营提出异议了。<br />
<br />
&nbsp; &nbsp; 沪宁高铁的票价高不高？<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;沪宁高铁票价为什么比既有线的客运票价高？我认为主要是高铁的成本和品质要高于既有铁路。这里有因为速度高、安全可靠性要求高、舒适度要求高等原则提高的建设成本，还有在铁路建设过程中，为了降低社会成本而增加的铁路建设成本。比如说，为了节约土地，高铁选择以桥代路；为了适应沿线城市发展需要，铁路线路有的要绕行，避开既有建筑，有的要尽量引入既有车站，既增加了铁路线路的长度，又一定程度上牺牲了运行速度，等等，这些都增加了铁路建造的成本。同时，高铁无论在速度方面，还是在舒适度方面，以及服务品质方面，和其他运输方式以及和传统铁路相比都有提高。此所谓“一分价钱一分货”。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;凭心而论，传统的铁路客运票价是长期偏低的。如目前沪宁航线的销售价格是320元，高铁146元的价格还不到民航的一半。作为一个企业，多付出了成本，实现了产品升级换代，价格适当提高是必然的，也是可以理解和接受的。<br />
<br />
&nbsp; &nbsp; 旅客究竟有没有“被高铁”？<br />
<br />
&nbsp; &nbsp; 根据近期国务院批准实施的《长江三角洲地区区域规划》，明确了长三角地区发展的战略定位，要求把长三角打造成为亚太地区重要的国际门户、全球重要的现代服务业和先进制造业中心、具有较强国际竞争力的世界级城市群。实现长三角区域发展规划目标，很重要的一点是构建现代化的交通运输体系，提供优质的运输服务和充足的运力保障。而长三角地区现有的铁路、公路运输能力已趋于饱和，特别是沪宁铁路的运能利用率已超过100%，运输能力已远远不能满足经济社会发展的需求。专家认为，沪宁高速铁路的建成，将极大地缓解地区交通运输紧张状况，既可以满足沿线城镇之间旅客快速出行需要，又可以使既有沪宁线腾出运力加开货物列车，满足区域内的货运需求。<br />
<br />
&nbsp; &nbsp; 沪宁高铁开通后，作为旅客还有没有其他选择？存在不存在“被高铁”？据我了解，沪宁高铁开通后，铁路部门考虑了旅客的不同需求，只是将既有线上的部分动车组列车转到高铁线上运行，既有线T字头、K字头列车和普通列车全部保留，同时还保留了部分动车组列车，旅客可以根据不同需要进行选择。另外，部分既有线上的长途动车组列车转到高铁线上运行，运行时间有了较大压缩，而列车的票价维持不变。从这一点来说，部分旅客可以享受到“提速不提价”的待遇。所以，我个人认为，旅客“被高铁”是不存在的。<br />
<br />
&nbsp; &nbsp; 总之，我认为，社会的发展进步是历史的必然，中国的交通事业也必将不断发展进步。正如公路上的马车终究会消失一样，铁路的运输装备必将会逐步更新换代，这体现了时代的进步。作为一名火车迷，我由衷希望铁路越来越好，我们的国家越来越好，我们的生活越来越好！</div>

							
							
							
							
