																											<div id="postmessage_705992" class="t_msgfont">沪宁高铁遇冷 平均每天每趟车仅卖出百张车票<br />
沪宁高铁旅客可在窗口购磁卡 出站直接“刷票”<br />
 来源：新华报业网-扬子晚报 2010年06月27日<br />
　　沪宁线班次密集，出行客习惯随到随走<br />
<br />
　　石小磊 徐媛园<br />
<br />
　　昨天，记者在南京火车站看到，排队买票的人不少，可买高铁票的人却不多。不过沪宁线的客流向来是“随到随走”，客流究竟如何还要等高铁开行后才知道。另外，记者了解到，以后旅客们可以通过自动售票机、在窗口购买磁卡、在窗口购买车票等多种购票方式登上沪宁高铁。不仅如此，出站时持有沪宁高铁车票的旅客再也不用检票出站了，可以直接“刷票”。<br />
<br />
　　售票<br />
<br />
　　窗口高铁票为何“遇冷”？<br />
<br />
　　可能是旅客习惯随到随走<br />
<br />
　　昨天下午3:00，记者在南京火车站的售票窗口守了40分钟，却只碰到2个来买高铁票的市民，都是“尝鲜”去的。南京市民冯力行先生就准备7月1日起个大早坐上首班车，预计要在8点37分到达上海虹桥火车站。“即便以最紧凑行程，从火车站连接通道赶到虹桥机场的公交换乘枢纽转乘世博4路，换乘加上行车时间，大概1个小时才能直达世博园6号门。”不过为了体验高铁冯先生还是决定买高铁票。<br />
<br />
　　不过记者在南京站售票厅里的屏幕上看到，7月1、2、3、4日高铁票的销售状况并不乐观。7月1日当天，G7003、G7025、G7011、G7013、G7014次（重联运行，核定1113/1218人）均余了一千多张车票。其余的高铁车次余票量在四五百张左右。2、3、4日的情况也是如此。就连最“火爆”的G7001次每天也只卖了100多张票。也就是说，平均每天每趟车只卖出了不到100张车票。不过沪宁线的客流向来是习惯“随到随走”，高铁的客流究竟如何，还是要等到开行当天才能看出来。<br />
<br />
　　购票<br />
<br />
　　高铁票可窗口购买、自助购买，还可刷卡乘车<br />
<br />
　　昨天12点，记者来到南京火车站一楼售票大厅时看到，所有的购票窗口前都排起了长队。位于大厅左侧，两台为沪宁高铁准备的自助售票机上还贴着泡沫保护膜，尚未使用。南京火车站内的这两台自助购票机与别的站点略有差别，自助购票机上，只设了银行卡插口、现金插口。记者了解到，目前，自助售票机上不但没有其余的购票方式，就连银行卡插口也还处于“摆设”状态，购票机投入使用后，市民暂时还只能通过现金插口用纸币购票，面额仅限100、50、20、10、5元的纸币，硬币不能用。<br />
<br />
　　据了解，沪宁高铁今后可能参考京津、广深城铁模式，为旅客提供刷卡乘车服务。在一些铁路部门经营的场所，旅客还可以持此卡消费。“就像现在的金陵通市民卡一样。”<br />
<br />
　　连日来，记者在江苏多个高铁站售票大厅内看到，不少高铁站里的自助售票机上的插口远多于南京站这两台机器的插口。除了铁路快通卡、银行卡、现金插口之外，还有一个二代身份证的感应区。乘客在自动售票机上用身份证购票时，需要将二代身份证进行扫描，完成支付后，旅客可以取磁票，也可以不取磁票，如果市民不取磁票，可直接持二代身份证检票上车。“这个感应区是为火车票实名制预留的，目前也都用不着。”铁路部门工作人员告诉记者。</div>

							
							
							
							
