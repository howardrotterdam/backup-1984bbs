													<h2>北京爱知行研究所所长万延海流亡美国</h2>
						
						
																											<div id="postmessage_607714" class="t_msgfont">今天的南华早报 <br />
<br />
Publication Date: May 10, 2010&nbsp;&nbsp;Publication: SCM&nbsp;&nbsp;Section/PageName: News/Nws_Page_1&nbsp;&nbsp;Page No: 01&nbsp;&nbsp;Edition/Zone: 2/1 <br />
 <br />
Headline: I had to leave, Aids activist says<br />
标题： 我不得不走，艾滋病活动家说<br />
Wan Yanhai , the mainland's leading activist on Aids and sexual minorities, has left China for the US with his family, saying harassment from the authorities has forced him into a self-imposed exile.<br />
万延海，大陆知名艾滋病及性少数人群维权人士，离开中国举家赴美。因为当局的骚扰迫使他自我流亡<br />
<br />
The director of the Aizhixing Institute - arguably the mainland's most vocal NGO - told the South China Morning Post that he and his family arrived in Los Angeles on Thursday night last week. For now, he is staying at a friend's place in Philadelphia.<br />
爱知行（可以说是中国最敢言的NGO）的理事,告诉南华早报，他和他的家人上周四到达洛杉矶。现在，他住在他朋友费城的住所。<br />
<br />
&quot;Before we left China, I was under a lot of pressure and was harassed by many [government] departments,&quot; Wan said in a phone interview late on Saturday.<br />
“我离开中国前，我受到大多的来自政府部门的压力和骚扰”。万在上周六接受电话采访时说。 <br />
<br />
Wan said not only had the local tax, commerce and industry bureaus investigated his group in recent months, but the fire department had also visited his office checking if any regulations had been breached. Beijing police also made dozens of phone calls to him last month and visited his home when he was out of town, he said.<br />
万说，不单单是地方税务工商部门近几个月调查他的组织，而且消防部门也来他的办公室检查有没有违反规章的地方。北京警方也在上个月打了几十个电话给他，并且在他出差时候去他家。<br />
 <br />
Phone calls to the departments went unanswered yesterday.<br />
昨天给他单位打电话没人接。<br />
 <br />
&quot;I felt my personal safety was at stake,&quot; he said. &quot;The mental pressure was just too much, so I've come out for a bit of breathing space.&quot;<br />
“我感到我的人身安全受到威胁”他说 “精神压力太大，所以去了个地方歇歇”<br />
 <br />
Wan said his family had not received political or financial assistance from the US. He said he and his wife were travelling on business visas that were issued in September when they went to the United States for an alumni reunion at Yale University. Wan was on the Yale World Fellows Programme in 2003.<br />
万说，他和他的家人没有受到美方的政治或者财产自助。他说他和他妻子用的签证是去年九月份赴美耶鲁大学同学聚会时候签发的商务签证。万是2003年耶鲁国际学者计划的成员。<br />
 <br />
Wan said he hoped to seek short-term research positions or fellowships at universities in the US to maintain his family. His four-year-old daughter is with them.<br />
万说他想在美国的大学里面申请短期的研究项目或者奖学金，由此来养活他的家人。她四岁的女儿也跟他在一起。<br />
 <br />
He said he would also seek collaboration with international organisations to look for ways for the Aizhixing Institute to continue receiving overseas financial aid.<br />
他说，他想寻求国际组织的合作来寻求使爱知行继续获得外国的资金援助。<br />
 <br />
Asked whether his departure would affect the morale of his staff and fellow Aids activists, Wan admitted this might happen but believed his staff would be capable of running projects smoothly in his absence.<br />
被问及他的离开是否影响他的工作人员和其他艾滋病活动者的士气，万承认这可能发生，但相信他的工作人员将在他缺席情况下，继续顺利进行项目。<br />
 <br />
&quot;Even if I hadn't left, I wouldn't be able to carry on working normally,&quot; he said. &quot;I kept getting phone calls from the police and five, six government departments are after me - I just could not concentrate on my work.&quot;<br />
“即使我没有离开，我也无法正常的工作”他说，“我每天要接到很多警察打来的电话，而且五六个政府部门总跟着我，我无法专心工作”<br />
 <br />
Wan said he did not know the reason behind the harassment but believed officials may be sending a message that they would like him to leave the country.<br />
万说他不知道这些骚扰背后的原因，但他相信这些官员在跟他发送一个信号就是他们希望万离开这个国家。<br />
 <br />
&quot;I'm not sure but it looks like they might be forcing me to leave ... the aim of the harassment is probably to give you pressure so you'll leave of your own accord,&quot; he said.<br />
“我不确定但是这看起来像迫使我离开...骚扰的目的可能是给我压力，让我自己找理由离开”<br />
 <br />
Wan's fellow Aids campaigner, Dr Gao Yaojie , the mainland's most high-profile HIV/Aids whistle-blower, also left China for the US last year.<br />
万的同事，艾滋病活动家，高耀洁老师，大陆最高调的HIV/Aids揭露者，去年离开中国去美国。<br />
<br />
 <br />
Another fellow activist, Hu Jia , was sentenced to three-and-a-half years in prison on the charge of &quot;inciting subversion of state authority&quot; in 2008.<br />
另一位活动家，胡佳，以“煽动和颠覆国家罪”在2008年被判处3年半有期徒刑。<br />
 <br />
While acknowledging that the central government has also made significant progress in its Aids projects, Wan said it did not like criticism, especially when it could potentially embarrass officials. Wan had accused provincial officials in Henan of covering up a blood-selling scandal in which at least 150,000 people became HIV-positive.<br />
虽然也承认中央政府在其艾滋病项目取得重大进展，但是万说，他们不喜欢批评，特别是当它可能会令官员非常为难的时候。万已经指控了一个掩盖卖血丑闻的河南省省级官员，导致至少有150,000人艾滋病毒抗体阳。<br />
 <br />
&quot;If you criticise them and tell the truth about the blood [contamination], they'll persecute you,&quot; he said.<br />
 “如果你批评他们，告诉有关的血液[污染]真相，他们会迫害你，”他说。<br />
<br />
Wan said he did not know how long he could stay in the US, but hoped to remain for two or three years. &quot;In China, I was a bird in a cage ... by leaving China I'll be out of their control and they might have more consideration for your influence,&quot; he said.<br />
万说，他不知道可以留在美国多久，但仍希望两年或者三年。 “在中国，我就是关在笼子里的鸟...而离开中国我就离开他们的他们控制的，他们可能对你的影响力有更多的考虑，”他说。<br />
