																											<div id="postmessage_607950" class="t_msgfont">Despite pressures, activist perseveres<br />
HIV/Aids campaigner continues his fight for a more democratic and fair China<br />
Verna Yu<br />
May 10, 2010 <br />
<br />
From a lone campaigner on a bicycle distributing Aids prevention fliers to a prominent activist closely watched by the government, Wan Yanhai has always given the impression of being a fearless maverick.<br />
<br />
But the bespectacled, slightly built 46-year-old former health official says he has always been very reserved.<br />
<br />
&quot;Many people say `Oh, you're so brave' but actually I'm a very timid person,&quot; Wan said in Hong Kong last week before he and his family departed for the United States. &quot;Even crossing a footbridge scares me.&quot;<br />
<br />
A passion to help Aids victims and an overriding sense of moral obligation are what prompted him to brave the daily pressure over the past two decades.<br />
<br />
Wan lost his job in the Health Ministry in 1992 when he launched the nation's first HIV/Aids counselling hotline, which officials closed the next year. Two years later, he founded the Aizhixing Institute, an independent NGO that aims to promote Aids awareness, advocate policy reforms and ensure that vulnerable communities have access to health care and legal protection. In 2008 Wan and the Aizhixing Institute helped found a centre for lesbians, gays, bisexuals and transsexuals in Beijing.<br />
<br />
From 1994 to 1997, Wan played a key role in exposing the connection between blood transfusions and an outbreak of Aids in Henan . More than 1 million people there sold blood to government-run clinics, which collected plasma during the 1990s. Due to improper procedures, at least 150,000 people became HIV positive.<br />
<br />
By bringing the blood-selling scandal to the attention of the media in 2001, Wan passed the point of no return. He has been detained three times in the past eight years and taken in for questioning on numerous occasions. In 2002, he was accused of &quot;revealing state secrets&quot; and detained for nearly a month after he posted on the internet a government report showing that Henan officials had ignored and covered up the blood contamination scandal.<br />
<br />
In late 2006, he was detained for three days after police ordered him to cancel a workshop that would have brought together HIV victims and NGO and legal professionals to discuss the spread of Aids through blood transfusions. He was detained overnight in late 2007 shortly after fellow Aids activist Hu Jia was arrested. Hu was later sentenced to 3-1/2 years for &quot;inciting subversion&quot;.<br />
<br />
Although Wan has received international acclaim for his work - he was the winner of the Human Rights Watch's award for action on HIV issues and human rights in 2002 and received the Fulbright New Century Scholar and Yale World Fellow awards - this has not made his work at home any easier.<br />
<br />
Police have always made their presence known around sensitive anniversaries, high-profile events, such as the Olympics, and international dignitaries' visits by following him everywhere and parking outside his home. Police have knocked on his door requesting to check his and his family's identity cards. His landlord has been pressured to stop renting a flat to him. He believes his phone calls are monitored.<br />
<br />
Asked why he would carry on in the face of such harsh conditions, Wan said he felt compelled to speak out for the underprivileged.<br />
<br />
&quot;It's my conscience. This society has too many problems and you have to have a sense of moral obligation towards it,&quot; he said. &quot;So many children have been infected because of blood contamination. There are too many disasters in our society. We must work hard to make changes. Otherwise, we - our families, our children - will be victims, too.&quot;<br />
<br />
Despite his strong sense of mission, Wan admitted the pressure had become too much recently.<br />
<br />
In March, the Beijing tax authorities sent inspectors to check Aizhixing's accounts - the second time in the past year and a half that they made surprise visits to the office. The same month, the Beijing Industry and Commerce Administrative Bureau sent inspectors to his office, and last month, the fire department visited.<br />
<br />
Recently local NGO partners have been told to cut contact with him. In March, police stormed into a lecture he was giving at a Guangzhou university and stopped him from delivering the rest of his speech.<br />
<br />
&quot;I have been under lots of pressure. I can't put my mind to rest,&quot; he said. &quot;I can't stand it any more. I'm not made of steel.&quot;<br />
<br />
Wan said he feared his group would meet the same fate as the Open Constitution Initiative, or Gongmeng, the independent legal aid centre that was raided and closed down last year over alleged tax evasion. Gongmeng was fined 1.42 million yuan (HK$1.62 million), and its founder, Xu Zhiyong , was detained for three weeks.<br />
<br />
Beijing does not allow independent NGOs to register as non-profit organisations without a caretaker, often a government department or government-backed agency. But that is nearly impossible, so most are forced to register as companies and must pay taxes.<br />
<br />
Wan said that by scrutinising Aizhixing's activities and stepping up harassment on him, the government was sending a message that if he continues his activism, what happened to Gongmeng would also happen to his organisation.<br />
<br />
&quot;If I carry on being unco-operative, they have ways to make me feel uncomfortable,&quot; he said.<br />
<br />
Asked whether he worried that he too would be arrested, Wan, the father of a four-year-old girl, said: &quot;Yes, of course. This is my No 1 concern.&quot;<br />
<br />
Aizhixing still collaborated with government agencies up until 2007, but Wan's campaign to seek compensation for the victims made the government feel his organisation posed too large a threat.<br />
<br />
Aizhixing's criticism of the government, promotion of human rights and largely overseas funding have made it unpopular with authorities, Wan said. It also represents an independent voice and has extensive networks with grass-roots NGOs - all of which make the government uneasy.<br />
<br />
&quot;I think it's their political fear. We were suing public hospitals and were receiving a lot of funding from abroad. I think they wanted to rein it in.&quot;<br />
<br />
The general environment for NGOs has become the harshest in recent years, Wan said.<br />
<br />
The State Administration of Foreign Exchange has this year tightened restrictions on overseas donations to independent domestic NGOs, requiring them to meet new conditions, among them the presentation of certificates of registration of the overseas donor organisations and a notarised donation agreement.<br />
<br />
Wan fears the new regulations will threaten the survival of the mainland's fledgling civil society.<br />
<br />
Analysts say Beijing is particularly sensitive to the growth of a robust, civil society, mindful of the role that independent groups, such as the labour union Solidarity in Poland, played in the meltdown of the Communist regime in Warsaw.<br />
<br />
But Wan said he is not anti-government. After all, his father joined the Communist Party as an idealistic young man and worked for the government all his life.<br />
<br />
&quot;We're not against the Communist Party. We have our views on democracy and human rights, but we're not against the Communist Party,&quot; he said.<br />
<br />
&quot;But I want to say to the government: China should be more democratic, China should protect human rights and the law.&quot;</div>

							
							
							
							
