<br />
　　由于对杂志未来的发展存在不同看法，公司内部的紧张关系已经持续了好几个月。作为《财经》杂志的创办人，胡舒立希望能够在很多话题上保持杂志报道的前沿性，包括腐败、污染、土地纠纷等敏感话题，这些话题往往会踩到新闻审查制度的底线。<br />
<br />
　　据称，在香港注册的发行商财讯传媒希望能将重点转移到争议较小的话题，譬如财经报道，并增加软文内容以换取更多收入。<br />
<br />
　　谈判仍在继续，但公司分裂对于新闻自由来说并非好事。<br />
<br />
　　尽管意见相左，以王波明为首的财讯传媒还是为《财经》提供了政治保护，与同类杂志相比，《财经》很少受到审查制度的粗暴对待。但由于今年有几个政治敏感的纪念日，再加上新疆的暴力骚乱，这种压力增加了。<br />
<br />
