<br />
　　据一名员工说，今年早些时候联办致《财经》编辑们的一份要求进一步缩小报导范围的备忘录加剧了紧张关系。这名员工说，这种矛盾因7月份的新疆骚乱事件而进一步升温，当时《财经》编辑得到了限制报导的通知，原因是这件事在政治上太过敏感。<br />
<br />
