<br />
　　据公司员工及知情人士透露，此次大规模辞职的起因是以总编辑胡舒立为首的《财经》员工和出版商中国证券市场研究设计中心(联办)(SEEC）在从商业策略到编辑理念等诸多方面意见失合。<br />
<br />
　　知情人士说，胡舒立目前正在和SEEC进行谈判，以期争取到对该杂志未来更大的控制权，但她有可能很快辞职，并可能引爆另一波以编辑团队为主的辞职风潮。一位知情者形容她和SEEC之间的谈判一点也不顺利。<br />
<br />
　　知情者说，《财经》杂志逾百名经营团队成员中约有三分之二在十一国庆节前夕递交了辞呈。<br />
<br />
　　胡舒立拒绝就此置评。记者未能联络到联办管理人士。《南华早报》(South China Morning Post)周一报导了这一集体辞职事件。<br />
<br />
　　《财经》此次人事地震的爆发正值中国传媒业在商业领域经历振荡之时，同时互联网也在挑战传统媒体的运作方式。中国政府一直在推动媒体领域变得更具市场导向性，即各家出版商为赢得读者的垂青而展开竞争，与此同时，中国政府还向大型国有媒体机构注入资金，推动其扩张。<br />
<br />
　　在媒体这一由政府主导的领域中，《财经》一直在探视政府底线，它所拥有的高度独立性非常罕见。胡舒立在1998年时创办了这份杂志，由于它发表了一些调查性报道和有时可能质疑官方观点的经济社会评论而引起了国内外的关注。事实证明，这一商业模式在吸引广告客户方面是成功的，并为管理其广告业务的财讯传媒集团有限公司(Seec Media Group Ltd.)带来了快速的收入增长。财讯传媒是联办旗下企业，在香港上市。<br />
<br />
　　胡舒立是一位曾在国有媒体打拼多年的资深人士，人们普遍认为她非常擅长揭发丑闻但又不至于像其他媒体那样被新闻检查部门处置，尽管在过去几年中《财经》也有几期杂志被政府撤下。<br />
<br />
　　王波明是前政府高官之子，交际广泛。他领导的联办组织架构纷繁复杂，但不受政府的直接控制，这在中国由政府控制的媒体业中殊不多见。背靠这座大山，《财经》得到了一些政治审查上的庇护，在中国能享受到这种待遇的出版物凤毛麟角。<br />
<br />
　　最近，胡舒立与联办之间就广告收入的分配和《财经》内容的性质产生了矛盾。<br />
<br />
