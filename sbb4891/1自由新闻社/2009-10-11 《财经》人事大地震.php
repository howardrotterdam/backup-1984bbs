009-10-12 | 《财经》大震荡：一个月后采编集体辞职 <br />
&nbsp; &nbsp; <br />
<br />
1 更震荡的在后面<br />
据搜狐等网媒报道，《财经》经营部门60多人已经集体辞职。这些人并非就此散去，而是去为另一个平台作准备。<br />
<br />
然而人们不知道的是，另一次更加震撼的辞职将发生在一个月后，届时，《财经》的主要资产――编辑和记者――也将集体辞职，转投到经营部门先期搭建好的平台之下。<br />
<br />
这意味着，这份全国最佳的财经类媒体进行了一次大搬家，将采访资源、广告资源完全从一个地方搬走，再到另一个地方重建。这样的折腾大概只有在中国这样的特色地域才能够见到。<br />
<br />
2 为什么要搬家，原因之一，财务不独立<br />
此次事件，之所以《财经》必须出走，是因为长期矛盾积累的结果。<br />
<br />
由于中国的特色，媒体必须寻找一个公有制挂靠单位，获得刊号才能出版。此前，《财经》挂靠的单位是中国证券市场研究设计中心（原名证券交易所研究设计联合办公室，简称联办），由于联办负责人王波明与主编胡舒立的关系，《财经》杂志获得了自主的采编权。<br />
<br />
但是，这种挂靠的体制也决定了《财经》对于自己的利润分配插不上话。目前财经年广告收入以亿为单位计算，然而这些钱最终到底属于《财经》，还是属于联办？至少从记者的收入就可看出端倪。<br />
<br />
《财经》记者的工资在业内并不算高，与其在业内崇高的名声很不相称，对此，记者的抱怨很多，但却一直得不到纠正。原因在于国内媒体的管制，即使记者不满薪水想离开，也找不到更好的平台，只能留下。<br />
<br />
但长此以往，激励机制不解决，迟早会带来大问题。<br />
<br />
3 为什么要搬家，原因之二，独立采编权的丧失<br />
在没有财政权的情况下，胡舒立之所以一直决定继续挂靠联办，是因为联办给了她自由采编权。<br />
<br />
在以往可以看到，联办对于《财经》采编是很少干涉的，这保证了《财经》成为了全国最有魄力的媒体。除了少数报道，比如《谁的鲁能》，由于直接牵扯到最高层腐败问题而不得不撤稿之外，一般的稿件都能够保持其犀利的风格。<br />
<br />
但进入今年之后，由于媒体环境的收紧，不管是联办还是联办的上级单位全国工商联，对于采编权的干涉却越来越多，短短的几个月，从央视到石首到新疆到通钢，《财经》已经不得不撤销或者推迟了多篇重量级稿件，对于采编权的干涉等于将双方之间最后的妥协给抽去了，这是胡舒立下决心离开的关键因素。<br />
<br />
4 如何搬家<br />
今天，财经经营人员的集体离职已经发生。一个月后，采编人员将离开，至此，留给联办老《财经》的将是一个空壳。<br />
<br />
如今，新的架构实际上已经建好，资金已经到位。当人员辞职后，马上就可以为新的平台工作，中间不会出现太大的间断。<br />
<br />
然而，这绝非简单地从联办转移到另一个挂靠单位，其背后还有另外的操作来避免曾经的问题。<br />
<br />
据悉，虽然有了新的挂靠单位，但出资的可能另有其人。厚朴投资的方风雷可能为投资人之一。引入外部投资后，新的挂靠单位可能只是提供挂靠资源，但在资本上却无法拥有话语权（满足于每年获取一定的红利），这能够保证新财经的独立运作。<br />
<br />
同时，据猜测，在引入外部投资时，其记者资源也可能会作为一部分资产入股，这意味着，至少有一批资深的采编人员有可能获得股权的激励。如果走出这一步，将是采编资源市场化的一次尝试，并能带来长期的激励机制。<br />
<br />
通过资本的运作，以及协议的签署，胡舒立将尽可能限制新挂靠单位的干扰，保证采编和财务的独立性。<br />
<br />
5 隐忧<br />
对于胡舒立而言，为了保持独立性这一步迟早会尝试。<br />
<br />
如果做好了，将成为在“中国特色体制”内尽量用市场化力量冲击体制阻力的样本。<br />
<br />
