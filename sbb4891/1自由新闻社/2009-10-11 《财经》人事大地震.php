<br />
　　其中一位人士说，这个媒体会保持《财经》的新闻报道传统，可能会包括一份杂志，但会比《财经》更多地关注互联网。<br />
<br />
　　与吴传晖一道辞职的一位前《财经》高级管理人员说，她和她的团队先离开是为了做新公司的筹备工作。<br />
<br />
　　还不清楚胡舒立是否能够象在联办时那样避开政府的干涉，按自己的意愿行事。但知情人士说，这家可能创办的媒体机构已经吸引了几家潜在投资者的兴趣。<br />
<br />
