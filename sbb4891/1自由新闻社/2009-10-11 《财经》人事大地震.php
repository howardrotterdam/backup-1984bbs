													<h2>《财经》团队另办新刊</h2>
						
						
																											<div id="postmessage_282530" class="t_msgfont">明报网上版 09.10.14<br />
--------------------------------------------------------------------------------<br />
<br />
【明报专讯】北京著名敢言媒体《财经》杂志发生重大人事变动，由于与资方「联办」就股权及办刊方针的矛盾激化，主编胡舒立将带领采编和经营部门大部分人员另起炉灶，创办新的财金类杂志《财新周刊》和财新网（暂名）。目前，已经离职的数十名经营人员在前总经理吴传晖率领下，已进入大望路新址筹备。据悉，新杂志刊号来自《浙江日报》报业集团，资金可能来自基金等多家机构，港商李泽楷据传也有兴趣入股。<br />
<br />
编辑部与资方矛盾激化<br />
<br />
《财经》杂志一直以敢言据称，其主办单位「联办」（中国证券市场研究设计中心，其前身是成立于1989年3月证券交易所研究设计联合办公室）长期备受中宣部等部门压力。据知情人士称，在央视大火、湖北石首事件、新疆「7．5」骚乱、通钢事件的报道中，「联办」及其上级，全国工商联曾多次要求《财经》编辑部交出审稿权未果。而编辑部则长期一直认为资方对《财经》投入不足，编辑部高层一直在公司内部无法享有股份，「加上审稿权之争，促成了最终的决裂。」<br />
<br />
56岁的胡舒立一直是内地传媒界的传奇人物，且与高层一些官员渊源颇深。有消息称，此次她另立门户，从浙江寻获新刊号，就获得曾任浙江省委书记的国家副主席习近平协助。据了解，编辑部的绝大多数编辑、记者将随胡离去，他们将在出完本月最后一期杂志后集体辞职。知情人士称，《财经》杂志的上半年纯利达5000多万港元，「经营和采编人员集体辞职，给《财经》只留下一具空壳。」<br />
<br />
《财经》杂志办公室如常运作。（明报记者摄）</div>

							
							
							
							
