<br />
　　“领导在辞职的时候没有给我们许诺什么，我们下一步的去向现在也没有确定。”<br />
<br />
　　该人士补充道：“实际上，当你合作很好的同事，你的上司和下属都辞职之后，你也会考虑做出一个选择。”<br />
<br />
　　有关《财经》震荡的原因，业界有着比较一致的说法。一位资深人士表示：“无论是胡舒立还是整个编辑群，在产品做成功的情况下希望提出股份激励的要求，但资方不予认同。”<br />
<br />
