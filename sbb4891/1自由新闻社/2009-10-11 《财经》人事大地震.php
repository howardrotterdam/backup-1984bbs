　　据传，吴传晖系联办系香港上市公司财讯传媒的执行董事，她的辞职已获集团批准，但尚未公告。“网上的说法基本就是事实。”联办集团一内部人士向本报记者表示。<br />
<br />
　　《财经》杂志经营系统共有90名员工，负责《财经》杂志和《财经网》的广告部、市场部、发行部以及技术部和产品部等多个部门。如果60人同时辞职，那么《财经》的运营势必将陷入大麻烦中。<br />
<br />
　　激励及理念问题促成决裂<br />
<br />
