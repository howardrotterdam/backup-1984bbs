　　据昨天的《南都娱乐周刊》报道，不久前，多年来以淳朴形象示人的满文军，因为在给妻子李俐的生日会上聚众吸毒，被警方拘留，满文军将被拘留14天，而他的妻子李俐因涉嫌提供毒品，有可能被判处6个月有期徒刑。涉案的毒品，主要成分是冰毒。冰毒的化学成分是甲基苯丙胺。<br />
　　<br />
　　这是旧闻了，不是这期《南都娱乐周刊》报道的重点，重点在于，报道指出，娱乐圈众多明星大腕深染毒瘾。报道未点名地、很含蓄地点出以下几位：<br />
　　<br />
　　王牌天后和老公：在结婚生子前，一直是圈中“毒”后，素喜大麻，有多年毒史，与其老公都是个中高手，不过因为怕怀孕对下一代健康有影响，才有所收敛，并且将注意力转移到佛学。老公在认识她之前，也经常在夜店吸食，曾被人目击。<br />
<br />
<br />
　　<br />
<br />
<br />
　　玉女导演：一直以来都以知性玉女形象示人，但也有吸毒经历。某第五代导演被抓现行之前，据说不久前也曾与她在另一Party共赏毒品。<br />
　　<br />
<br />
<br />
　　卷发导演：虽然已经为自己的吸毒行为付出了代价，被警方抓捕过一次，也主动表示自己并没有毒瘾，也愿意戒毒，但后来还是被目击有精神恍惚、言行古怪的现象，怀疑并没有彻底戒掉。<br />
　　<br />
<br />
<br />
　　摇滚老炮：之前曾被另一老炮炮轰“糟蹋处女”，曾有姑娘为其跳楼自杀，但他除了有“集邮”爱好以外，也是“毒海”老炮，而吸毒现象在摇滚圈实在是稀松平常。<br />
　　<br />
<br />
<br />
