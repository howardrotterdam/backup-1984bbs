　　<br />
<br />
<br />
　　摇滚帅哥：另有一曾演唱过西藏题材的摇滚老帅哥，也有吸毒史，不过目前似乎有所收敛，醉心练习瑜伽。<br />
　 　<br />
<br />
<br />
　　妇女主任、高音老公：被称为是朝阳区明星们的知心大姐，外号“妇女主任”，公众形象一直很好，但她与高音老公也是“满家帮”的成员，不过运气太好，阴差阳错没被抓到。<br />
　　<br />
<br />
<br />
　　大眼女星：跟东北歌后、王牌天后是姐妹淘，据知情人称，吸毒目的是为了减肥，但吸食后精神恍惚，曾主动向某音乐才子示好，但才子考虑到她可能刚刚“吸了粉儿”，就拒绝了，不想惹麻烦。<br />
　　<br />
<br />
<br />
　　杂耍男歌手：“满家帮”成员之一，好歌不多，曾因配唱根据民国经典言情小说改编的电视剧主题歌而闻名，2004年，曾有网友称，看见他在青岛因吸毒被警方带走。<br />
　　<br />
<br />
<br />
