																											<div id="postmessage_540956" class="t_msgfont">赵连海的无罪辩护词（节选）<br />
<br />
就北京市大兴区人民检察院“京大检刑诉[2010]0043号起诉书”指控我涉嫌寻衅滋事罪的起诉，我将做一些必要的说明及辩护。.....<br />
&nbsp; &nbsp;&nbsp;&nbsp;自2008年9月11日三聚氰胺事件发生以来，我作为维权带头人被警方多次谈话，但从未提及我曾经的事情构成犯罪，另在2009年7月后因准备进行结石宝宝周年纪念，与警方再多次频繁解除沟通，在7月至9月11日结石宝宝周年长达两个月与警方多次接触的时间里，也多次谈及曾经的这些事情也从未指出我这些事情构成犯罪，并一再好意的劝告我千万别有什么冲动的事情发生构成犯罪。这些情况也证实我曾经的这些事情并没有被警方认为是犯罪，我也没有因这些事情收到过警告或提示说我构成犯罪，让我不能理解并痛心的是事后竟指控我那些在警方严密监控下的事情构成犯罪。<br />
&nbsp; &nbsp;我们身为被伤害的孩子们的父母们，我们是希望切实解决发生在我们孩子们身上的伤害以及有效保障我们孩子们一生的健康。迄今为止，不仅仅中国甚至全世界也没有任何一个权威机构敢断言三聚氰胺对孩子们的伤害仅仅是肾结石，而已有的研究和相关报道都是让我们紧张并焦虑的，我们至今担心一些隐性的伤害将会影响孩子们一生的健康。<br />
&nbsp; &nbsp;我们身为孩子的家长最关心孩子们生命健康及一生有效地保障，我们的维权也一直是合理合法且理性文明的，我们也从没有指控的所谓“非法聚集，呼喊口号，起哄闹事”等这些无中生有的事情。<br />
&nbsp; &nbsp;我身为一名结石宝宝的父亲及社会的一员，我坚信我自三聚氰胺事件以来所做的事情没有犯罪，我反而要自豪骄傲于我所做的所有努力，我在尽力尽职我应肩负的责任，我也期望自己曾经的所有努力与付出能对我们社会的进步有所推动，并且我坚信我所做的一切无愧于我自己的良心、无愧于我们身处的这个时代，我认为我所做的都是正确的，反而我倒认为如果我不去做这些事情，我将认为是一个罪人有愧于自己的良知与灵魂，也有愧有罪于我身处的时代及我们的后代，我们努力让我们的思想与行为更加高尚，坚定我们正确的做人信念，并期待能对社会进步有积极有益的作用。我们身处这个时代，有责任坚持正确的事情并让人为的错误尽量减少。我们作为社会的一员，有不可推卸的责任甚至使命为我们的后代努力营造一个更有道德、更公正、更公平、更美好的社会环境。<br />
&nbsp; &nbsp;我因为维权及协助别人报案被冠以莫须有的罪名而失去自由，在此我期忘这样的伤害与委屈尽快结束，并期望是非被颠倒的事情不要再发生。<br />
&nbsp; &nbsp;在此，我要说：如果维权有罪，那势必会助长利欲熏心的奸商继续丧尽天良、肆无忌惮的将自己的利益建立在残害他人的基础上，我们本已日渐沦丧的社会将会变成何等扭曲的样子。<br />
&nbsp; &nbsp;在此，我更要说：如果报案及揭示犯罪有罪,将会就此扼杀正直的行为,将会纵容更多的罪犯肆无忌惮的为所欲为,如果这样,我们每个人都将处于危险的社会之中,正义与勇敢将逐渐不复存在,想必这是每个具有良知善德的人都不想看到的。<br />
&nbsp; &nbsp;今天的我,由于错误的指控被强行关押失去了自由,我所能期待的也只有祈盼法律应有的公平公正来为我主持正义还我于清白。在此也真诚期望我们的政府相关部门能正视已犯下的错误，不要一错再错。<br />
&nbsp; &nbsp;我坚信，正义与真理的光芒必将照耀我们这个国度的每个角落，而我所能做的也只能是倔强的坚持，不为别的，仅仅为了我自己良心与灵魂的安稳，以及为了我们的后代们能生活在一个具有优良品质的社会里，更为了我们这个民族，我们这个国度能以健康的体魄继续存在于世界。<br />
&nbsp; &nbsp;&nbsp; &nbsp;我坚信我所做的一切都没有犯罪，也期望拥有权力的人能拥有高尚的人格与美德，想想所有被三聚氰胺毒奶残害的孩子们，秉持惩恶扬善的准则，做出无愧于这个国度、无愧于良知与灵魂及无愧于子孙后代的决定。这样，我们才无愧于我们做为一个生命在这个世界、在这个国度的意义。<br />
&nbsp; &nbsp;&nbsp; &nbsp;最后，我要再重申，我没有进行起诉书内指控的犯罪行为，我坚信我所做的一切都是身为一名公民应担当的一份责任，我坚信自己所做的事情都是文明且没有错误的，我也祈盼自己的努力能让社会有所进步。<br />
&nbsp; &nbsp;&nbsp; &nbsp;为了将正直、良知的品德保留在我们的生命及灵魂中，我们只能坚定正确的信念和准则，否则将动摇我们正直的心灵并错误的影响我们的后代，那样，我们才将是罪人。<br />
&nbsp; &nbsp;&nbsp; &nbsp;综上所述及事实，我坚信我无罪！<br />
