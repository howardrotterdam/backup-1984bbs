													<h2>大陆台资加薪 引中共情报机关关注</h2>
						
						
																											<div id="postmessage_682150" class="t_msgfont">2005年，博讯连载神秘作家杨恒均的《致命武器》，小说标榜惊险政治间谍的故事情节，可这本书引发北京内部高度重视的是作者以小说的形式警告了当局：民工可能成为终结北京政权的最后一根稻草。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;作者巧妙地安排了台湾当局与商人利用大陆两个亿的“盲流”（现在的农民工）制造混乱，最终实施了台湾的“决战境外”计划。这当然不是空穴来风，实际上，后来透露的两岸消息显示，台湾情报当局确实一直严密关注在大陆的台商情况，以及农民工的动向。 (博讯 boxun.com)<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 《致命武器》里描述了这样一个可怕景象：当北京决定对台湾动武，要“解放台湾”的时候，台湾当局只好孤注一掷，下令台商同时撤资，离开大陆――结果会如何？根据作者的统计，广东福建沿海，靠台资厂以及相关企业生存的农民工超过两千万人，而这些人几乎都是生活在最底层的民众，一旦他们失去工作，失去工厂，两千万人，结果会如何可想而知，大陆区区百万军队用来“维稳”都不够，何来军力解放台湾？<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 这当然是杨恒均虚构的小说情节，然而，书中披露的这群在自己国土上流浪、暂住的农民工，并不永远是最好欺负的，使用起来得心应手的“机器人”，他们不同于农民，他们比祖祖辈辈栓在土地上的农民要强，他们“获得了自由与权利”――“获得了到处被奴役自由，以及享受不平等对待的权利”，他们“脚踏不平路”、“眼见天下不公事”（《致命武器》语），可由于资讯的发达，以及眼前对比的强烈，他们实际上已经成为“播种机、宣传队”，他们的愤怒与不满一触即发，只是时候没到而已。书中虚构的好像不可能发生的故事情节，让所有读者感觉到并不陌生。据说，北京从2005年后，开始关注农民工（盲流），就和这本小说引发的思考不无关系。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 可是六年后，《致命武器》里描写的台商抛弃农民工的情节没有出现，最近却出现了台商郭台铭大幅加薪的事件，而这个事件却同样引起北京相关情报部门的高度重视与担忧。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 北京记者昨天从北京公安部相关人士获得的消息显示，来自情报部门的情报最近多次警告有关政法部门（包括公安）：台商“不理性”的加薪，可能引起席卷全国的工人罢工，要求效仿加薪的要求可能会以极端的形式出现在各地，并有向内地蔓延的趋势。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 公安部有关人士解读“不想性”的意思是：脱离中国国情，大幅增加工人待遇，而且经过媒体宣传，让各地工人感觉到自己没有加薪是受到不公正待遇。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 这位给博讯提高消息的公安部四局处长说，广东本田罢工要求加薪获得满足，郭台铭突然连续加薪，实际涨幅高达50%以上，实在让北京感觉惶恐。他说，如果一些台商不理性加薪，甚至人为“恶意”加薪，这消息又无法控制，全国会出现什么情况？<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 他对博讯记者表示，目前工资偏低的是几个亿的底层民众，又不仅仅是一两个厂？一两个厂加薪容易，全部加薪的话，中国将会一夜之间处于崩溃的边缘。博讯记者听到这里很有点不懂，就问他为何这样说，他解释道，这个时候，大家都不会再装糊涂，中国当今所有经济繁荣难道不都是靠这些廉价的劳动力支撑的？真要为所有加薪买单的不会只是台商、私人企业，而是国家。国家现在财大气粗，不就是建立在这些跳楼而亡的农民工身上？<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 博讯记者询问，最近公布的公安部要连续七个月严打，是否和工人要求加薪的抗争运动有关，这位处长先是否认 说这是重庆打黑引起的，早就定下的。在博讯记者再三追问后，他后来不得不承认，对于一些鼓噪工人抗争要求加薪的人，肯定也是这次严打首要的对象，因为他们制造的不稳定，要远远超过任何凶残的罪犯。不能不打。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 博讯记者北京报道。<br />
