													<h2>前中国工程院院长证实中国在建航母</h2>
						
						
																											<div id="postmessage_367454" class="t_msgfont">前中国工程院院长证实中国在建航母<br />
星岛环球网 <a href="http://www.stnn.cc" target="_blank">www.stnn.cc</a> 2009-12-11 <br />
宋健，中国前国务委员，国家科委主任，中国工程院院长在香港中文大学演讲<br />
<br />
 <br />
<br />
　　星岛环球网消息：中国是否在建造航空母舰？各种猜测持续已久。中国前国务委员宋健说，他认为中国的确在建造航空母舰，而且数量还不只一艘。宋健还表示，中国正在凭借其巨大的财力和人材资源在科技方面全面追赶世界先进国家。<br />
<br />
　　中国在建造几艘航母<br />
<br />
　　美国之音报道，国际上对中国是否正在制造航空母舰有着浓厚的兴趣，有关传言和猜测流传很久，但一直没有得到权威消息的证实。曾经担任中国国家科委主任和中国工程院院长的宋健博士9日在香港中文大学发表科教兴国演讲的时候就这个问题提供了新的内容。<br />
<br />
　　宋健说：“报上老说航空母舰，我也没有确切的消息，但是我猜他们在干，而且(用了)相当大的力量，也还不只是一艘，他们准备干几艘。”<br />
<br />
　　宋健这位控制论和系统工程与航天航空技术专家虽然给自己下面的说法冠以“猜测”的称号，但分析人士认为，凭着宋健的作为前国家领导人的身份和对科技界的了解，他是不会随便乱说的。而且宋健所讲内容的真实性也是听众难以否认的。<br />
<br />
　　江泽民总书记时拍板上马<br />
<br />
　　宋健说，中国有个商人从俄罗斯购买了一条废弃的航母“瓦良格”号，把它运回大连，在上面开餐馆，办娱乐。但后来，中国当局就把这艘船买走了，用来作为参照，进行航母研制工作。<br />
<br />
　　宋健说：“他们就把这个要下来。据说，这是江泽民总书记的意思。他说，给孩子们说说，‘你们别玩了，拿来做，’现在准备以它作为试验，作为样舰。”<br />
<br />
　　宋：财力增加，费用不再是障碍<br />
<br />
　　宋健表示，过去中国不敢建造航母是因为费用太高，每艘航母的建造费用大约需要500亿元人民币，需要10年的时间。这位退休的中共高级官员对目前中国的财力状况感到满意，他认为，建造费用昂贵的障碍现在已经不存在了。<br />
<br />
　　宋健说：“现在我们的财政真是不错，现在稳定在每年三万亿。这是中央财政，是温家宝总理手上有三万亿。如果按照汇率计算，我们只有美国的1/3。但是，按照购买力和中国的工资比，差不多一个美元顶一个中国一快钱。所以还行，拿出500亿或者1000亿都比较容易了。”<br />
<br />
　　在80年代担任国务委员、国家科委主任，执掌中国科技决策权的宋健博士在2003年退休。他年近80，但身体看上去相当硬朗，步履轻快，面色红润，谈笑风生。他认为，经过30多年的快速经济发展，中国的科教能力也今非昔比，已经迈上了一个新台阶。<br />
<br />
　　中国科技加快赶超条件具备<br />
<br />
　　宋健介绍说，中国现在的大学数量比1950年时增加了10倍以上，总数达2263所，每年大学招生人数更是增长了两百多倍，从50年的三万人增加到现在的650万人，累计培养大学生8000多万人。中国的科技人才总数为3800万，跟美国不相上下。<br />
<br />
　　随着财力、人力和物力的迅速增长，宋健认为，中国现在有能力加快速度全面赶超世界先进水平。他说，中国不仅已经开始建造航空母舰，而且在建造大型客运飞机和大型运输机方面也有很大的进展。在超级计算机、生物工程、资源勘探等许多领域中，中国都达到了世界最高水平。<br />
<br />
　　宋：盗窃情报，没有的事<br />
<br />
　　对于中国在某些领域的快速进步，特别是在军事技术方面的快速发展，西方一些机构认为，这是中国科技间谍的功劳。有关中国科技和商业间谍的报道时常会在美国媒体上出现。宋健对此专门做出做出了说明：<br />
<br />
　　宋健说：“我是做过中国潜艇发射的核导弹项目的副总设计师。我真正知道核潜艇导弹的事情。一个美国人叫考克斯，他写过一个报告，说中国的(潜艇)导弹是偷美国的。绝对没有那回事。”<br />
<br />
　　中国的导弹外形有点像美国的原因，宋健说，是因为中国设计人员从香港购买了世界武器杂志。宋健他们在研制的过程中参照了杂志的图片，在试验中采用了图片的尺寸。宋健坚决表示，中国方面没有通过间谍手段盗取过美国的导弹设计。<br />
<br />
　　在回顾中国科技发展历史的时候，宋健认为，成功的根本原因是实行了开放。开放可以让中国走出去学习先进技术，招募尖端人材。中国从欧洲空中客车公司、从美国的波音飞机公司和其它许多高技术公司招募了很多专家，从而能够加快相关科研项目的进展，使许多看上去不可能事情变成可能。</div>

							
							
							
							
