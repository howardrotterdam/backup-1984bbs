<br />
&nbsp; &nbsp; 2010年4月8日傍晚，某省高院副院长刘宏所乘小车与一女士所驾小车发生无意轻微刮蹭，结果双方发生争执，情急之下，女士打电话叫来的她的丈夫。双方争执升级，女士丈夫一拳打向刘宏，刘宏随即倒地磕在硬物上……经急诊抢救，确诊为脑干损伤……经过一天的抢救……无效……死亡……年仅60岁！<br />
<br />
&nbsp; &nbsp; 自2001年4月被中纪委点将，抽调到省里办&quot;2&#8226;23&quot;专案，刘宏，这位曾任7年反贪局长的衡水市副检察长一鸣惊人。此后，在河北甚至全国反贪领域，刘宏都是响当当的名字。<br />
<br />
　　刘宏，沧州任丘人，1950年生，1989年正团职转业到衡水市人民检察院，先后任起诉处侦查员，反贪局局长、检察院党组副书记兼副检察长。<br />
　　2001年4月被抽调到“2&#8226;23”专案，任侦查一组组长。<br />
　　2002年，荣立中纪委、监察部、最高人民法院、最高人民检察院、河北省委、河北省政府等六部委个人一等功。<br />
　　2003年当选燕赵都市报主办的“感动河北年度人物”。<br />
　　2004年被河北省检察院记一等功，被最高人民检察院评为“全国优秀侦查员”。<br />
　　2005年10月，调入河北省检察院，任副厅级检察员。<br />
　　河北省纪委“12&#8226;9”张全专案领导小组副组长。<br />
&nbsp; &nbsp; 2008年调入省高院，任副院长。<br />
<br />
<br />
<br />
