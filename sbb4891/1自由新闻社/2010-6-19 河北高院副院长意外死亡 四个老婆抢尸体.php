&nbsp; &nbsp; 四个老婆，大老婆有两个女儿，大女儿刘妍，二女儿刘倩（因超生，一直寄养在别人名下）；二老婆梁娟有一儿一女，市房管局工作，以前在车站理发，2001年刘宏通过关系安排其调入市房管局，2008年两人秘密领取结婚证，梁娟平日处事高调，开好车穿名牌，少与一般人来往；老三领孩子住在石家庄，老四领孩子住在衡水市桃城区税务局家属院。<br />
