<br />
&nbsp; &nbsp; 滥用权力<br />
&nbsp; &nbsp; 衡水市检察院副检察长刘宏，工作之余酷爱堆长城，衡水市国税局局长霍某是其牌友，其间霍向刘诉说近日总有人向上面反映他有经济问题，问刘能否帮忙摆平，刘满口答应下来。随后不久，衡水市国税局召开干部职工大会，衡水市检察院副检察长刘宏作为特邀领导出席。刘简短的发言起到了非同凡响的效果，在致辞中，刘说“霍局长工作认真是个不错的干部，不过最近有些人反映霍局长有贪污腐败等经济问题，我想这是捕风捉影的，哪些人反映了哪些问题，材料最终都到了我这里，我清楚的很，我就是衡水的克格勃”。此言一出，果然天下太平。不过，几个月后，霍还是被免去局长职务，巧合的是，会议室内任命决定刚一宣布，大家还没来得及鼓掌，大门口停靠的四辆出租车上下来十来个年轻人一起燃放鞭炮，致使会议室内几分钟内听不到讲话声，待鞭炮燃放完小伙子们乘车离去后，任免会议继续召开。<br />
