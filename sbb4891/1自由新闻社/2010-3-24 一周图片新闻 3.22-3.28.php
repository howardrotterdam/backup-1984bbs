													<h2>黄果树瀑布减流3/4 中国多地持续干旱天气</h2>
						
						
																											<div id="postmessage_530674" class="t_msgfont">.<br />
<br />
.<br />
　　<br />
<br />
　　中国多地持续干旱天气，贵州省黄果树大瀑布大“瘦身”，漂流景区水位也大幅度下降为平时的1/4左右。 <br />
