.<br />
<br />
<br />
　　上周末，一场沙尘暴席卷了北京及北方大部份地区，随后黄沙南下，导致香港的空气质量“严重恶化”，亚洲的其他国家也笼罩在灰蒙蒙的天空下。<br />
<br />
