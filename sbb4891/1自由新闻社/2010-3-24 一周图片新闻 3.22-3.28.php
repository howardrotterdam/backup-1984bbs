<br />
　　AFP/Getty Images<br />
<br />
　　北京的天空变成了洋红色，建筑物已经完全看不见了。图为，沙尘暴期间北京的一名垒球运动员在击球。<br />
<br />
<br />
<br />
<br />
<br />
