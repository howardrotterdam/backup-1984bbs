<br />
　　贵州省持续干旱造成黄果树大瀑布大“瘦身”，漂流景区水位也大幅度下降为平时的1/4左右。图为3月21日，一名游客在拍摄黄果树瀑布。 <br />
<br />
<br />
<br />
<br />
　　<img src="http://img4.cache.netease.com/photo/0001/2010-03-22/62C995U400AN0001.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　3月21日，受干旱影响的中国第一大瀑布贵州黄果树瀑布纤弱细腰的身材。<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://img3.cache.netease.com/photo/0001/2010-03-22/62C9952N00AN0001.JPG" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　近日，受去冬今春西南地区干旱及三峡大坝水位消落影响，长江、嘉陵江重庆段水位仍在下降。截至3月21日14时，长江重庆段寸滩水位为159.47米；嘉陵江重庆段北碚水位为173.47米。 图为3月21日嘉陵江重庆段大面积裸露的河滩。<br />
