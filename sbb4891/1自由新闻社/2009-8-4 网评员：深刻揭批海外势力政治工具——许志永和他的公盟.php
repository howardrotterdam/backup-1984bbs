公盟无疑还是以公司的形态存在的 ，不具备合法社团的资质，也自然不是公益性组织。那么，它的生存状态是什么样的？是什么样的资金在支撑这个组织的运行就值得玩味了。如果像许志永等所言确是一家公益组织的话，许志永的个人收入从哪里来？这个组织完全是他和同道们在投入吗？从各种新闻报道来看，许志永没投入什么资金，而大多数来自社会各界的募捐，甚至来自有着法律诉求的当事人，那么，一边喊着为弱势群体主张权益，一方面又号召他们捐钱，其公益性何在？<br />
