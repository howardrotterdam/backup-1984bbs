																											<div id="postmessage_190225" class="t_msgfont">萧梁：接受资助甘当海外势力政治工具 公益维权面具遮盖滔天私利――许志永和公盟的必然下场<br />
<br />
一直以法治和法律名义制造热点新闻的许志永和公盟终于成了舆论漩涡的中心。7月中旬，因偷漏税被处以142万元罚款，同时“公盟”公司被北京市民政局勒令关闭，这件看起来不足以成为新闻的执法行为却引发了轩然大波。许志永带头喊冤“苍天在上”，拥趸们煽动民众声援，不明就里的网友在诱导之下想当然地将此事件理解为政府部门“暴力执法”。因此，舆论呈现出一边倒的伪正义态势，理智的声音却廖廖无几。<br />
<br />
<br />
暂且不说税收和民政部门查处一家公司的违法行为有何不妥，目前言论的严重失衡才最令人担忧，种种迹象表明，以炒作博得公众同情是许志永及其创办的“公盟”的一贯手段，此次遭受查处恰好又成了其“申张正义”的借口，大肆喊冤使普通民众忽略了违法行为本身，转而把注意力放到“执法不公”和“社会良心”等方面。许在事发后的一篇博客中公然写道：“142万的处罚，也许对很多企业而言都算不得什么，可是对于公盟而言，这是残忍而邪恶的，这不是对公盟的处罚，这是对毒奶粉受害的孩子、打工子弟学校的孩子、遭遇物业公司欺负的业主、为内心正义奔走呼号的上访者……是对千千万万最需要帮助的无权无势者的处罚，这处罚丧尽天良！”许志永把政府部门的执法行为称之为“残忍而邪恶的”，把公司违法所受的处罚转嫁到“毒奶粉受害的孩子、打工子弟以学校的孩子……”身上，实在有些无耻，完全是以赖皮手段在混淆概念。<br />
<br />
<br />
<br />
事实上，如果我们仔细剖析许志永及其“公盟”的行径，寻求深层次的真相，就会发现其背后的触目惊心和险恶用心。<br />
一、借法律事件炒作出名，目的是塑造个人影响力。<br />
<br />
一、借法律事件炒作出名，目的是塑造个人影响力。<br />
许志永从一个籍籍无名的大学教师和执业律师成为公众眼中的名人，发端于著名的孙志刚事件。正是这个法律界和所谓民间维权领域“里程碑”式的事件让许志永尝到了“新闻炒作”的甜头，成就了他日后的“伟大壮举”。我们不能忽略以法治事件推动完善法制建设的社会作用，但是以偏盖全地夸大其作用便是蛊惑人心了。“名人效应”为许志永迅速集聚了影响力，在市场经济中，影响力可以通过多种途径转化为财富，并呈现几何式的良性膨胀。因而，不难发现之后很多轰动性事件中都出现了许志永的身影，为弱势群体打官司和借势媒体使他成了正义的化身，于是噱头越来越多，他个人的影响力越来越大，并通过组建不合法的公益组织进一步扩大影响力，达到其个人目的。<br />
二、打民间维权旗号，逾越法律和制度，挑战政府公信力。<br />
<br />
二、打民间维权旗号，逾越法律和制度，挑战政府公信力。<br />
在百度词条的搜索中，关于许志永的介绍是：“中国人权活动家，维权律师，北京市海淀区十三、十四届人大代表，北京邮电大学教师，民权团体‘公盟’创办人。”这个复杂的身份结构直接体现了许志永本人的复杂性，其中人权活动家、民间维权领袖、公盟创始人等字眼早已超越了其学者和人大代表的身份，这种半官半民半学半公益的多个名头成了其变脸的道具。<br />
说句实话，如果许志永果真致力于民间维权事业，为何不干脆辞了多重职业而彻底去搞维权？如果他真想要推动法制和社会进步，为何不倡导以正常渠道维权和行使人民代表的身份为老百姓说话，反而以“民间维权领袖”自居，专揭社会的阴暗面，迎合个别事件中民众的非正常心态？<br />
诚然，经济转型过程中，腐败行为令人深恶痛绝，社会基层聚集了很多社会矛盾，群体事件时有发生。此时，从大局出发，维护社会稳定是重中之重，个别政府部门和官员的行为不能代表整个执政群体，正确引导群体的情绪，通过法制手段疏导并解决问题才是正途。然而，许志永等大力倡导的民间维权，摒弃了人民代表建言献策的合法渠道，常以指责政府部门的错误为主线，以告倒政府部门为荣，企图通过新闻炒作逼政府部门就范，恰恰是在误导民众强化与政府的对立情绪。<br />
解决社会矛盾应寻求社会主义制度和法律框架下的合法渠道，然而许志永最开始创建的“阳光宪政”，从名字上就透露着西方民主宪政的意味，已然在抨击现行制度和法律体系，企图以个人英雄主义和西方价值观去推动法律和制度层面的改革，这种作法并非会达到其所宣扬的“社会进步”的结果，反而有可能激化社会矛盾，造成社会不稳定。<br />
