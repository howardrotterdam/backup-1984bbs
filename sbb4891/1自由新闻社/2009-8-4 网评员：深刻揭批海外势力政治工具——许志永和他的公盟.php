在如今合法的公益组织也屡屡被曝套取资金等丑闻之时，公盟这样半地下的组织会是纯粹的公益维权吗？公益的尺度如何衡量？<br />
早有人曝料说许志永是在借公益维权的名头牟取私利，一位曾找到公盟寻求法律援助的当事人说，公益就只是一个说法了，没有钱是打不了官司的，他们（公盟）会选择能产生社会轰动效果的案件，或者偏重于经济维权的案件，可以提取高额的回扣，和其他律师团体没什么两样，公益性案件就是做做样子。公盟近期不断炒作民营企业家受不白之冤等案件，其实目的很简单，帮这些企业家洗脱罪责名利双收。<br />
