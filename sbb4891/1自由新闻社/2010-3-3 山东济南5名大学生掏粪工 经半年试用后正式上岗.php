													<h2>济南大学生淘粪工正式上岗</h2>
						
						
																											<div id="postmessage_502810" class="t_msgfont">济南大学生淘粪工正式上岗<br />
<br />
2010年03月04日10:39<br />
<br />
<img src="http://img.hexun.com/2010-03-04/122858075.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
面对电视记者的镜头，正在挑粪的张婷显得有点不好意思。丁捷摄<br />
<br />
<img src="http://img.hexun.com/2010-03-04/122858076.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
大学生淘粪工的拜师仪式。丁捷摄<br />
<br />
<img src="http://img.hexun.com/2010-03-04/122858077.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
大学生淘粪工王延峰和城肥二处负责人签订聘用合同。丁捷摄<br />
<br />
　　3月2日上午，在山东省济南市城肥清运管理二处一间会议室，5名大学生淘粪工和城肥二处的负责人签订了聘用合同。这5名大学生淘粪工是去年从 391名应聘者中经过严格考核选拔出来的。经过半年的试用期，他们正式成为了城肥清运队伍中的新成员。而他们的工种，和应聘时相比没有改变，仍然是淘粪工。<br />
<br />
　　不过，记者采访了解到，大学生淘粪工的合同期限是5年，但并不意味着这5年他们必须从事这一工种，如果表现得好，他们完全可以进管理层。<br />
<br />
　　拜师学淘粪<br />
　　挑得起百十斤担子<br />
<br />
　　在大学生淘粪工签订聘用合同的同时，还举行了一个简短的拜师仪式。济南市城肥清运管理二处为这些大学生选定了老的淘粪工作为指导老师，这些大学生向他们的指导老师献上鲜花，并且深深鞠躬。<br />
　　这些老师都是工作多年的老淘粪工。张婷的指导老师卢爱章是城肥二处清洁四队四班老班长、济南市五一劳动奖章获得者。邢鸿雁的指导老师张兆斌曾获得先进工作者的称号，宿昊的指导老师付树华还是省劳动模范。<br />
<br />
北园大街上的一座居民楼旁，宿昊和同事们清理居民化粪池。宿昊站在池前，先用铁钩将井盖掀起，然后将大橡皮管子放到化粪池里，接着按下车上的一个开关……橡皮管子足有百十斤重，要双手环抱着才能插进化粪池。尽管只穿着一件单衣，可不一会儿，他就累得大汗淋漓。<br />
<br />
　　25岁的宿昊目前是济南市城管局城肥清运管理二处3队4班的一名淘粪工。从早上8 时一直到中午12时，宿昊和同事一共抽了四车粪水。他说，“虽说我是个男的，但刚开始干的时候，每天都浑身疼，现在倒也觉不出什么了。”<br />
<br />
　　“你看，淘粪时勺子要贴着旱厕的壁，这样淘得满，而且也不容易洒漏。”在济南市甸柳小区一处旱厕内，老淘粪工卢爱章正在教张婷如何淘粪。<br />
<br />
　　22岁的女孩子张婷对这份工作的感触则更深，“我主要负责甸柳庄小区的旱厕清挖，平均一上午要挑十几挑粪水。去年第一天干的时候，百十斤的挑子，我半挑都没挑起来。不过现在我挑一整挑绝对没问题。”说起这些，张婷的脸颊上露出了淡淡的笑意。<br />
<br />
　　对于这些大学生来说，从去年9月份开始上岗，经过这几个月的淘粪工作，加上老淘粪工的言传身教，他们已经初步摸到了一些门道。<br />
<br />
　　身高1米74的大学生淘粪工孟佳介绍，她个子较高，老淘粪工专门给她扁担上的链子加长了一块。链子长了，扁担上下有适度的颤悠，挑起来感觉轻一些。反之，如果扁担两端的链子很短，没有颤悠看起来好像很稳定，但是那样肩膀会感觉很沉。<br />
<br />
　　“现在这5名大学生中，有3个男生已经‘顶’起来了。”济南市城肥二处一名负责人介绍，2名女大学生上手相对慢一些。<br />
<br />
　　应聘淘粪工<br />
<br />
　　大学生成单位品牌<br />
<br />
　　“真没想到这些孩子很快就融入了角色，丝毫没一点大学生的架子。”张婷所在班的班长卢爱章说。这位上世纪60年代出生的老淘粪工告诉记者，在他的印象中，“80后”往往是怕苦怕累、只会享受的一代，没想到新来的这几个年轻人都表现得“不孬”，让他感到后继有人。<br />
<br />
　　济南淘粪工的招考始于去年6月。当时济南市环卫局所属的3个单位一共对外招聘9名工作人员，其中城肥清运管理二处对外招聘5名粪便清除工―俗称 “淘粪工”。<br />
<br />
　　据了解，不少求职者都表示不愿当淘粪工，但听说淘粪工是事业单位编制时，不少求职者表示愿意考虑这一职位。目前，淘粪工的工资由政府财政拨款支付。现在一个老的淘粪工，每月的工资福利收入可达3000元。<br />
<br />
　　由于具有事业单位编制，收入有保障，所以对这个岗位的报考异常火爆，首日就有152人参与竞争。到报名截止时，一共有391人报名，其中还有1 名研究生，大学生更是比比皆是。6月21日，报考人员统一参加了笔试，试题内容有逻辑、判断、语文、时事等。<br />
<br />
　　笔试成绩公布后，有15人入围面试名单，但随后有2人弃权，其中1人是笔试最高分。济南市人事局随即对面试名单进行了递补。<br />
<br />
　　最终，张婷、王延峰、孟佳、宿昊、邢鸿雁5名大学生被录取，其中张婷和孟佳都是女生。这5名大学生年龄在23岁至26岁之间，4名是本科学历，一名为专科学历。孟佳、邢鸿雁都是共产党员。孟佳是学信息与计算机科学的，曾作为志愿者去章丘农村支教。王延峰学习的是土木工程，曾经在一家大型钢铁企业工作过。<br />
<br />
　　老淘粪工张兆斌说，此前城肥二处已经多年不招人，单位职工的平均年龄在50岁以上，早年社会的偏见也给不少人心里留下了阴影。而这批大学生同事的到来，也让他换了“老脑筋”。“连大学生都能干这活，说明很多人已经转变了观念，这也是社会进步的一种表现。他们逐渐成为我们单位对外服务的品牌。”<br />
<br />
　　而在单位团总支书记张晓芳的眼中，除了工作之外，这几位年轻人还多才多艺，成为整个单位一抹亮色。今年2月，在单位慰问一线职工的春节联谊晚会上，他们每个人都上台表演了节目，其中大学生王延峰生龙活虎地跳了一段街舞，孟佳和宿昊参加表演了《三句半》，而张婷则伴着熟悉的旋律为众人献上了一曲《隐形的翅膀》，“我知道我一直有双隐形的翅膀，带我飞，给我希望。”张婷说，其中的这句歌词仿佛专为现在的她而写。每每唱起，心中就会涌出一个念头：只要努力，就有希望。</div>

							
							
							
							
