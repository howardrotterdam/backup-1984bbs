　　未必5年都淘粪<br />
<br />
　　仿佛一切早已尘埃落定，但一切又还在进行中。至今，人们对大学生干淘粪工还是或多或少有些看法。孟佳说，刚开始自己挑起挑子走在街上，所有的目光都盯着她看，让她很不好意思。早晨进居民家服务，站在大门口，她有时连大声喊门的勇气都没有。“小姑娘怎么干这个？小姑娘还有干这个的？”这两句别人的议论也是张婷经常听到的。“为什么原来大家说我们‘80后’怕苦怕累，但现在我们不怕苦不怕累，大家又不理解了呢？”<br />
<br />
　　让孟佳兴奋的是，在这个岗位干了一个月后，自己领到了第一份薪水。虽然不是太高，但也是自己辛勤所得。拿着这笔钱，她给父母买了新衣和新鞋，让二老欣喜不已。<br />
<br />
　　宿昊也坦言，自己大学学的是法律，当初在报考淘粪工这个岗位时，他曾犹豫了3天。“我的父亲是一位教师，他的思想比较开明。他告诉我，如果连这份工作你都能干好，那么就没有你干不成的事情。我想，我是在靠一双手吃饭，这不丢人。”<br />
<br />
　　在淘粪工岗位上要干多少年？未来的道路有没有规划？5名大学生都表示，现在只想干好眼前的活儿，至于以后怎么发展还真没有考虑过。<br />
　　据了解，5名大学生签订的《山东省事业单位聘用合同》 规定，城肥二处聘请5名大学生从事“粪便净化处理工作”，期限为5年。<br />
<br />
　　济南市城肥二处有关负责人表示，签订合同，意味着这5名大学生已经成为城肥二处的正式职工，按照合同5年之内不准变动工作，但是这并不意味着这 5名大学生5年之内必须要做淘粪工。<br />
<br />
　　“应该说我们单位还是缺少人才，特别是高学历的人才。”这位负责人说，国家培养一名大学生不容易，这些大学生刚刚踏入社会，无论从事什么工种，都是一种锻炼。将来，不排除根据这些大学生的表现来对他们的工作岗位进行调整。如果大学生表现得好，管理层的大门也向他们敞开。<br />
<br />
　　讲 述<br />
　　“衣服味儿太大，不敢穿进屋”<br />
<br />
　　由于众人的过度关注，济南市城肥清运管理二处招聘的5名大学生淘粪工去年上岗后一度销声匿迹。从去年9月正式报到到现在，已经半年了。这5名大学生平时的淘粪工作怎么干？生活又是什么样子？<br />
<br />
　　“这5个人被录用后，我们就安排了日常的工作内容，挖粪、挑粪这些活每个人都干了。他们的精神状态都很好，都明白自己工作之后要干什么。”一位老淘粪工说。<br />
<br />
　　“夏天干完之后必须回去洗衣服。”女大学生淘粪工孟佳告诉记者，在冬天，她会把淘粪时所穿的棉衣放在储藏室，这样气味就不会散发到室内了。到了夏天，淘粪时穿的衣服则一天一洗。<br />
<br />
　　“刚开始挑的时候肩膀都磨出了红印和血泡。”孟佳介绍，自己平时早上6点多开始干活，干完就已经11点半了。<br />
<br />
　　张婷的家住在济大路，而单位却在解放路上，上班要穿济南城而过。每天早上5点半，小姑娘就早早爬起来吃完饭，赶早班公交车到单位上班，近半年以来风雨无阻，没有一次迟到。<br />
<br />
　　“清挖旱厕的时候，你戴口罩吗？”记者问，对此张婷坦言：“从一开始我就不戴口罩，因为影响工作。”<br />
<br />
　　上岗伊始，张婷的工作就和老职工一样：挖粪、挑粪。在工作的这段时间内，张婷说，她心理和体力上都发生了一些变化。一开始，她担心工作干不好，现在她能坦然面对了，认为女生也可以当好“淘粪工”。<br />
<br />
　　张婷说，在报考“淘粪工”这个岗位时，她就有了充分的心理准备，她父母也支持她。她妈妈还鼓励她，考上了就要好好干，行行出状元。在工作中，她也吃了一些苦。第一天上班，老职工为了照顾她，让她挑五六十斤的担子，可没想到，回家的当晚，她肩膀就疼得躺不下，也睡不着。经过半个月的努力，她现在可以和老职工一样挑100多斤的担子了。<br />
<br />
　　“以后天天干这份工作，我去挑粪的时候，如果有人问我就说，没人问那就算了。每个人都有自己的生活，没必要在意别人的看法。走自己的路，让别人说去吧。”一个男大学生淘粪工表示，同样一件事，不同人的看法不一样，说法也不一样。工作究竟好不好，自己明白就行了，没必要听别人说什么。<br />
<br />
　　不过，淘粪工的身份还是会让他碰上尴尬。“很多大人教育孩子都说，‘你要是不好好学习，以后就淘粪去’。”至今，就连他的母亲对他的选择还感到很不理解，对外界的看法也心存疑虑。<br />
<br />
　　春节同学聚会，大家都会谈到彼此的工作。一名淘粪工说，春节同学聚餐时，有同学提及工作的事，自己便随口说出“淘粪”两字，有人赶紧插一句“咱一会儿再谈这个话题”。<br />
<br />
【来源：北京日报】 (责任编辑：汪升兰)</div>

							
							
							
							
