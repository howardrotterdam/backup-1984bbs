													<h2>国开行原副行长王益受审 赵薇等两著名女星涉案</h2>
						
						
																											<div id="postmessage_539625" class="t_msgfont">来源：新华网 2010年03月30日<br />
　　4月1日是“愚人节”，也是国家开发银行原副行长王益的生日，有人戏称其“愚人王益”。然后，今年的愚人节，注定与王益已经度过的53个生日截然不同，因为他已经从副部级官员沦为阶下囚。今天上午，王益在北京市一中院出庭受审，检方指控其受贿1190多万元。令人震惊的是，王益涉嫌犯罪一案，有两个著名女明星牵涉其中。<br />
<br />
　　牵出两大女明星<br />
<br />
　　港商作证<br />
<br />
　　上午9时30分，头发灰白的王益穿着一件灰色棉夹克，被两名法警押进法庭。王益看上去精神状态很好，他先向旁听席上扫了一眼，但是他的家人并未前来旁听。检方指控称，王益受贿金额为1190万元。其第一笔受贿金额来自香港商人李涛。李涛和王益早就认识，他在湖南承接一个高速公路项目时，通过王益获得银行贷款，并向王益行贿538万元。目前，李涛也已被捕。<br />
<br />
　　王益受贿的第二笔金额，系通过其弟弟王磊，为云南一个叫周某的商人办理贷款，并分12次通过王磊或自己直接收受周某贿赂630万元左右。王益受贿的第三笔金额，是帮助河南一个叫王磊(此人非王益弟弟)的人为其他企业办理贷款，此王磊得了300万元的好处费，而王益向此王磊要了30万元。<br />
<br />
　　在检方举证时，李涛通过录像作证。李涛在证言中说，他在深圳开过一家百货公司，公司开业典礼时，王益前来出席，并带来女明星赵薇(音)，典礼结束后，李涛给了赵30万元的出场费。此外，著名主持人刘芳菲(音)在北京买了一套房，月供上万元。于是，王益让李涛给了刘200万元，用于偿还房贷。后来王益事发被调查时，他让刘把200万元还给李涛，并让刘给了李涛4万元利息。此外，王益带孩子到美国拉斯韦加斯过年时，李涛给王益孩子的压岁钱就是1万美元。<br />
<br />
　　在法庭上，王益认罪态度非常好，他说：“这些事都属实，这是我利用直接权力以及权力以外的影响力，为他人谋取利益，我也得了好处。”<br />
<br />
　　据悉，王益没有聘请辩护律师，法院为其指定了著名的刑辩律师许兰亭代为辩护。截至记者发稿时，许兰亭询问了王益关于其到案情况以及其收受李涛贿赂的相关问题，王益均一一作答。<br />
<br />
　　另类王益<br />
<br />
　　权力造就文人气质？<br />
<br />
　　在法庭上，王益没有丝毫的文人气质，有的只是对自己所涉案件的悔恨。但是，案发前，王益不但有证券会原副主席、国开行副行长的耀人光环，而且还被北京大学光华管理学院聘为博士生导师，并协助创办了光华EMBA。<br />
<br />
　　在《光华校友通讯》上，有一篇“我们敬爱的王益老师”，文中称“对于王益老师的身份，我们真的很难表述。金融家？音乐家？历史学人？还是&quot;王益老师&quot;这个光华人对他的称谓更为亲切。对于王益老师的气质，我们真的很难分辨。诗人的率真和激情；历史学人的深邃和内敛；金融家的果断和严谨；我们更愿意把他视作有哲学思辨、历史钩沉和文学冲动的一个可亲可敬的老师和朋友”。<br />
<br />
　　有人称，王益其实欠缺经济学理论功底，但其地位帮助光华EMBA打响了头炮。上世纪90年代末，光华EMBA一万美元的学费一点也不便宜，但两个班中的金融班，40多人大都是王益组织进来的，这些人都是冲着王益的官员背景而来。<br />
<br />
　　在陶醉于自己“教授”身份之余，王益在前几年突然来了音乐灵感。据王益自己说，2002年他和一群年轻人在青藏高原旅行，祖国山河的雄伟壮丽，突然激活了他埋藏已久的“音乐基因”。他一路游走一路歌唱，等回到北京，他已经唱出了《未来我们相爱》、《奥运情怀》、《彩虹下的你》等歌曲。2006年，王益完成《神州颂》的创作，并于12月9日由中国交响乐团在北京首演，之后一发不可收拾。2007年2月，修改后的《神州颂》在北京演出三场，此后《神州颂》第三版到上海、杭州、广州、珠海、深圳、成都巡演，半年内演了17场。<br />
<br />
　　然而，事后曝出内幕，原来《神州颂》的观众，基本上是各证券公司等和王益有关的企业所组织的员工。而为《神州颂》演出做策划的，是一个叫魏锋的人，此人是已故“涌金系”掌门人魏东的哥哥。<br />
<br />
　　王益被捕后，《神州颂》再也没有演出过。当初王益把创作《神州颂》归结于6年北大教育给他的影响，归结于对祖国的、民族的爱。然而，站在被告席上的王益，究竟是文人，还是用权力谋取的“文人”名声？J161<br />
<br />
　　未解谜团<br />
<br />
　　无一证券公司涉案？<br />
<br />
　　王益被捕前，外界就传言其与一些证券公司的违规操作有关。比如魏东自杀后，人人怀疑“涌金系”与王益之间的重大关联，涌金系旗下的国金证券董事长雷波，是王益在证监会时期的秘书；魏东的哥哥魏锋是王益《神州颂》演唱会的策划人之一。<br />
<br />
　　此外，有多家媒体曾报道称，公司上市可以通过两种途径，一是满足新股发行的种种条件，比如连续3年盈利等等，通过证监会发审委审核批准进行IPO；或者借壳上市，借壳上市须得通过证监会重组审核委员会审核，借壳上市的公司沿用原来壳公司的上市代码。但是，“没有3年连续盈利”不符合IPO基本条件，也没有沿用壳公司原上市代码因而不算借壳的太平洋证券，凭借着两个与上市无关的部门――证监会机构部和证监会办公厅的相关批复，史无前例离奇上市。太平洋证券上市后，股东斩获约40倍的暴利，其股东身影中不乏王益亲属，如其弟王磊、其妹王薇。<br />
<br />
　　然而，目前可以肯定的是，在检方的指控中，王益涉嫌受贿事由，均是为他人办理贷款提供帮助，而与证券公司无关。(杨昌平) (来源：北京晚报)</div>

							
							
							
							
