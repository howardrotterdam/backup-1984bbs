																											<div id="postmessage_470691" class="t_msgfont"><strong>卡大遭中共报复 600中国学生前途堪忧</strong><br />
<br />
卡尔加里大学被中国政府从其认可机构名单中除名。大学方面表示，中国政府此举可能与达赖喇嘛到访有关，去年9月，卡尔加里大学授於在卡城访问的西藏精神领袖达赖喇嘛法学荣誉博士学位。 <br />
　　<br />
据《卡尔加里先锋报》4日报导，卡尔加里大学被中国教育部从其认可大学名单上剔除了下来。卡尔加里大学发言人图纳尔（Colleen Tuner）对此表示：「这有点突然，有点让人失望，我们不想让我们的学生受到负面影响。」 <br />
　　<br />
去年9月，西藏精神领袖达赖喇嘛受邀访问了卡尔加里，并与卡尔加里大学校长哈维・温加藤（Harvey Weingarten）的会见。达赖喇嘛被授予法学荣誉博士学位。<br />
<br />
遭到中国政府除名，卡尔加里大学事先没有得到中国官方的任何通知。图纳尔担心，学校正在就此事对在读的中国学生以及卡大校友的影响作出评估。<br />
<br />
目前，大约有600名中国籍学生（大陆和香港）在卡尔加里大学学习，许多人担心学成回国後不被认可。 <br />
　　<br />
一个名叫Jessie Zhang的中国学生说：「从我个人来说，我想留在这里，但对於那些别无选择的交流学生来说会怎样呢？他们在这里是不是浪费了钱财和时间？」<br />
<br />
中国驻卡尔加里领事馆发言人侯丹娜（Danna Hou）表示，这并非突然的决定，而是与去年发生的某些事情有关。她说：「他们（卡尔加里大学）应该知道为什么，并应该知道某些事情的后果。」<br />
<br />
卡尔加里大学（University of Calgary）位於加拿大艾伯塔省卡尔加里的西北部，是加拿大前排名前七的研究性大学之一。它的前身是阿尔伯塔大学卡尔加里分校。1966年开始独立授予学生学位。2004年拥有总计27,928名学生，其中来自87个国家的上千名国际学生。<br />
<br />
大学提供100多个学科，可以授予本科丶硕士丶博士学位。学校还拥有一个医学院，一个商学院，一个法学院，等。近年来，卡大与中国北大丶清华丶中山丶同济等25所大学建立了交流关系。<br />
