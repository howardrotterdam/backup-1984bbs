<br />
汪洋说，中国宪法明确规定，中国公民享有言论自由。中国从来不缺少言论自由。我们今天能够平等、坦率地在这里交流，本身就是言论自由的表现。有些你们认为有言论自由的国家，政府内阁频繁更替，议会里拳脚相向，如果这样的“言论自由”发生在有13亿人口的中国，后果将不堪设想。中国的“文化大革命”期间曾经有“大鸣、大放、大字报、大辩论”的言论自由，其结果是许多人的基本人权得不到尊重，国民经济到了崩溃的边缘。“不同国家对言论自由有不同的标准，我们不愿把自己的标准强加给别人，也不希望别人把标准强加给我们。”<br />
。。。。。。<br />
