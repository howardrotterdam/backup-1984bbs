广州日报7月31日报道 昨日下午3时，广州珠岛宾馆红棉厅，来自美、英、法、德、日、韩、新加坡、中国香港等8个国家和地区的境外主流媒体41名记者把长枪短炮架了起来。他们此次到广东采访，有一个共同的“条件”：“要见广东省委书记汪洋”。<br />
。。。。。。<br />
德国明镜周刊记者华格纳紧接着提问：“在中国，腐败产生的原因，是不是因为中国政治缺乏言论自由?”<br />
