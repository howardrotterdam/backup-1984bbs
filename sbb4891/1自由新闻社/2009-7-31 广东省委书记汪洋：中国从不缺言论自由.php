													<h2>广东省委书记汪洋：中国从不缺言论自由</h2>
						
						
																											<div id="postmessage_185303" class="t_msgfont">广州日报7月31日报道 昨日下午3时，广州珠岛宾馆红棉厅，来自美、英、法、德、日、韩、新加坡、中国香港等8个国家和地区的境外主流媒体41名记者把长枪短炮架了起来。他们此次到广东采访，有一个共同的“条件”：“要见广东省委书记汪洋”。<br />
<br />
“客人来了，作为广东这个大家庭的‘主人’，如果我不见诸位，不太礼貌。我愿意和我的同事们一起回答大家的问题，但希望你们的问题不要让我们太为难。”汪洋幽默的开场白，让记者们会心地笑了。<br />
<br />
“广东正努力扩大在国内市场上的份额，争取对未来经济发展的主动权。”<br />
<br />
现场提问非常热烈。<br />
<br />
路透社记者请汪洋预测下半年广东经济发展趋势。<br />
<br />
“虽然目前广东经济企稳向好，但下半年的经济发展还有很多不确定因素。因为广东是经济外向度很高的省份，经济走势很大程度上取决于世界经济形势的发展变化。现在连保罗・克鲁格曼这样世界顶尖的经济学家都很难判断世界经济走势，我们更多冀望国际经济形势已触底并逐步回升。目前广东一方面在继续巩固和开拓国际市场，另一方面也正努力扩大在国内市场上的份额，以争取对未来经济发展更多的主动权。”<br />
<br />
凤凰卫视记者提问：“广东在粤港合作方面有什么新动作?”<br />
<br />
汪洋说，除了粤港政府间合作机制外，两地也正在建立民间的沟通渠道。广东准备成立一个由各界人士组成的团体，与香港大珠三角商务委员会等民间团体加强交流合作，调动两地民间力量，成为政府间沟通的重要补充。目前，广东正在和香港特区政府协商，按《珠江三角洲地区改革发展规划纲要》的要求，制定粤港合作框架协议。粤港合作的具体新动作到时就会揭晓。<br />
<br />
“曾经和我们一起工作的同事，却因腐败问题被抓进牢狱，这对我们每一个正在工作的人都是一种很好的警示。”<br />
<br />
英国金融时报记者把焦点对准腐败问题。“随着世界经济发展，广东不断扩大开放，腐败的机会在不断滋生，近期广东就发生了陈绍基、许宗衡案件。这两个案件彼此是否有联系?执政党应该怎么处理腐败问题?”<br />
<br />
汪洋同意这位英国记者提问中的观点。“迄今为止，任何一个国家在发展中都会出现腐败案件。防止腐败是摆在世界上所有执政党和政府面前的一个艰巨任务。最近美国新泽西发生的腐败案件涉及范围就很大，这说明在你们看来民主与法制都比较健全的国家，腐败依然存在。因此，在广东出现陈绍基、许宗衡案件并不是很稀奇的事情。”<br />
<br />
汪洋接着回答：“至于这两个案件之间有无联系，在中央纪委没有公布案件调查结果之前，我和诸位一样，都只能凭自己的判断。据我个人了解，两个案件没有直接联系。”汪洋说，作为执政党，我们将认真总结经验、教训，从惩治、教育两个方面不断完善反腐倡廉制度体系。“曾经和我们一起工作的同事，却因腐败被抓进牢狱。这对我们每一个正在工作的人都是一种很好的警示。”<br />
<br />
省纪委副书记丘海补充说：“在国外朋友眼里，可能感觉这两个案件影响很大。但实际上，这几个月来广东非常平稳，案件并没有影响广东应对国际金融危机和改革发展。”<br />
<br />
德国明镜周刊记者华格纳紧接着提问：“在中国，腐败产生的原因，是不是因为中国政治缺乏言论自由?”<br />
<br />
对此，汪洋立即给予了否定的回答。“你的问题提得很坦率，我也坦率地回答你――我不赞成你的判断。任何自由都是有条件的，那就是不以损害别人的利益为前提。”<br />
<br />
汪洋说，中国宪法明确规定，中国公民享有言论自由。中国从来不缺少言论自由。我们今天能够平等、坦率地在这里交流，本身就是言论自由的表现。有些你们认为有言论自由的国家，政府内阁频繁更替，议会里拳脚相向，如果这样的“言论自由”发生在有13 亿人口的中国，后果将不堪设想。中国的“文化大革命”期间曾经有“大鸣、大放、大字报、大辩论”的言论自由，其结果是许多人的基本人权得不到尊重，国民经济到了崩溃的边缘。“不同国家对言论自由有不同的标准，我们不愿把自己的标准强加给别人，也不希望别人把标准强加给我们。”<br />
<br />
“只有加快产业结构调整，再加上必要的环保措施，广东环境才能得到根本改观。”<br />
<br />
法新社记者问，广州南沙中科炼化一体化项目最近是不是有新变化?广东如何做到把发展经济与保护环境更好地结合起来?<br />
<br />
汪洋说，在充分听取了老百姓和专家意见的基础上，经过艰苦的努力和大量的协调工作，广东省已经决定把原选址在南沙的中科炼化一体化项目迁出珠三角地区，这个行动本身体现了广东对环境保护的重视。当前广东要解决好环保问题，最重要的任务是推进产业结构调整，下决心转移甚至关闭那些高能耗、高污染的项目，并适当减少珠三角人口以减少生活污染。只有加快产业结构调整，再加上必要的环保措施，广东环境才能得到根本改观。<br />
<br />
美联社常驻广州记者傅维廉提了一个有点“影响团结”的问题：“我听说您与黄华华省长看待经济发展的角度不同。您希望企业用创新赢得竞争力，但黄省长更倾向保护珠三角的劳动密集型产业。听说黄省长认为您走得过快了一点。”<br />
<br />
“我坦率地告诉你，这是你在广州听到的‘马路消息’。”汪洋幽默的回答，立即在会场上引发了大家的笑声。<br />
<br />
汪洋接着说：“我倒可以披露一下在中国党政是怎么分工的。作为省委书记，在经济工作中主要考虑谋篇布局、把握方向。因此，大家可以经常听到我在那些需要确定前进方向的地方阐述观点。黄华华同志作为省长更多是侧重操作层面的事情，考虑把省委、省政府重要的战略部署落实到工作中。事实上，当前广东所有涉及建设现代产业体系、加强自主创新的政策、措施都是由黄华华省长牵头制定并推动落实的。”<br />
<br />
远东经济评论记者关心广东正在进行的经济转型和产业升级会引发社会的“不稳定”。<br />
<br />
汪洋说，广东在产业转型升级的过程中，必然会引发一些利益冲突，客观上存在不稳定因素。不过，自从国际金融危机发生以来，广东正在进行的产业升级，还没有引发过大规模的社会不稳定事件。这是因为有几个关键因素保证了广东的产业升级能在相对稳定的环境中进行：一是广东是中国市场经济发育最充分的地区之一。企业主和员工对企业在市场经济竞争中被迫关闭、破产，认为是正常的市场竞争现象，在观念上没有抵触情绪，不会为难政府。二是广东主要通过政策杠杆，充分发挥市场作用引导企业转型升级，而不是强迫企业转型升级，因此企业不会跟政府形成矛盾。三是这次国际金融危机形成的倒逼机制使产业转型升级成为企业的自觉选择，不创新、不转型，企业就没有出路。正是有了这几个有利因素，广东在产业转型升级中虽会有利益冲突，但总体上是在有序中进行。<br />
<br />
韶关“6・26”集体斗殴事件是由于员工间冲突而造成的治安案件，司法部门将依法作出判决。<br />
<br />
日本共同社记者问，最近在新疆乌鲁木齐市发生的打砸抢烧严重暴力事件与广东省韶关市一家玩具厂员工间冲突似乎有一定关联。从这个事件来看，中国政府沿用多年的民族政策是否有需要修正的地方?<br />
<br />
汪洋说，中国是一个多民族国家。中国的党和政府历来高度重视民族团结。中国现行的民族政策总结了中国的历史经验，适应中国国情。当然，从哲学的意义上讲，任何适用的东西都是相对而不是绝对的。就不断发展变化的民族关系而言，任何政策都应该随着时代变化而不断调整和完善。这一条，不仅适应民族政策，也几乎适应所有其他政策。<br />
<br />
