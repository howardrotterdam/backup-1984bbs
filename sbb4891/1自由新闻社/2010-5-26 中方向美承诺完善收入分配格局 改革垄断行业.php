													<h2>中方向美承诺完善收入分配格局 改革垄断行业</h2>
						
						
																											<div id="postmessage_639576" class="t_msgfont">中方向美承诺完善收入分配格局 改革垄断行业<br />
2010年05月26日01:56人民网-人民日报我要评论(113) 字号：T|T <br />
　　胡锦涛主席特别代表、中国国务院副总理王岐山和巴拉克・奥巴马总统特别代表、美国财政部长蒂莫西・盖特纳在北京共同主持了第二轮中美战略与经济对话框架下的经济对话。对话于5月25日闭幕。两国的部长和高级官员参加了对话。<br />
<br />
　　双方重申将继续履行2009年7月27―28日在华盛顿举行的首轮中美战略与经济对话所作承诺，继续深化经济合作，促进强劲、平衡和可持续的全球经济增长。<br />
<br />
　　一、促进强劲的经济复苏和更加持续、平衡的经济增长<br />
<br />
　　2009年7月首轮中美战略与经济对话举行时，双方承诺继续实施强有力的刺激措施，以帮助中美两国和世界经济度过全球金融危机。双方还承诺采取措施转变两国经济增长方式，促进世界经济在完全复苏后强劲、可持续地增长。<br />
<br />
　　自首轮中美战略与经济对话以来，世界经济已度过最困难的时刻，复苏势头日益巩固。去年7月，中方承诺增强国内消费在促进增长方面的作用，并在其后实现了2009年消费对国内生产总值增长贡献率的大幅提高。美方在首轮中美战略与经济对话上承诺采取措施增加国内储蓄，迄今，私人储蓄已经有所提高。同时，美方一直在为实现承诺采取后续行动，通过实施包括全面医改在内的措施，来确保长期财政可持续性，促进公共储蓄。<br />
<br />
　　双方承诺就财政、货币和结构改革等问题加强宏观经济政策对话与协调。双方认为，宏观经济政策应考虑各个经济体的具体经济形势，同时考虑全球经济形势，并与Ｇ20匹兹堡峰会“强劲、可持续和平衡增长框架”下有关承诺保持一致。为此，<br />
<br />
　　●随着中国经济复苏的巩固，中国将通过实施促进消费的财政和货币政策，继续进行结构改革来保障消费在经济增长中发挥不断扩大的拉动作用。<br />
<br />
　　●随着美国私人需求的恢复，美国财政政策的重点将从保持总体需求和支持就业转向减少中期联邦政府财政赤字和保证长期财政可持续性。<br />
<br />
　　●我们将共同努力采取调整国内需求和相关价格的政策，以促进更加可持续和平衡的贸易和增长。<br />
<br />
　　双方认识到，两国在实现经济可持续发展方面负有共同责任。双方同意在去年取得进展的基础上，各自推动建立更平衡的经济增长模式。美国未来经济增长将更多地依靠国内储蓄而不是外国储蓄。美国致力于走出一条负责任财政的道路。奥巴马总统预算案已经制定出具体措施，将通过推行历史性的财政约束在未来十年内使财政赤字削减一万亿美元。这些措施包括：在三年内冻结非安全性可支配支出；要求金融服务业全部偿还问题资产救助计划的成本；2001―2003年间对年收入超过25万美元家庭的减税政策将在到期后失效；取消对低效化石燃料补贴的资金。美国国会已经通过了“量入为出”法案，该法案要求国会如果增加1美元的非紧急支出、或减少1美元的税收，都必须在其他方面节省1美元的支出。一旦美国经济复苏，全美财政责任和改革委员会将制定应对财政风险的政策，并使债务占ＧＤＰ比例在经济复苏后稳定在可接受的水平。<br />
<br />
　　通过启动减缓医疗成本增长的改革，美国已经采取重大举动，解决其长期财政面临的一个主要威胁。此外，美方将进一步采取措施，通过扩大就业为基础的退休储蓄工具，完善私人储蓄的激励措施。<br />
<br />
　　中方将继续努力，增加国内消费对经济增长的贡献。中方将完善国民收入分配格局，逐渐增加居民收入占国民收入的比重。为有效促进创造就业，中方将加速发展服务业，加快垄断行业改革，为中小企业提供更多融资渠道。为进一步改善民生，中方将继续加强社会保障体系建设，包括扩大新型农村社会养老保险试点范围，加快建立健全养老社会服务体系，多渠道增加社会保障基金等。<br />
<br />
　　双方认识到中小企业对中美两国经济增长和可持续发展的重要性，承诺共同签署中小企业合作协议，并举办促进中美中小企业合作论坛。双方还承诺深化中美地方层级经济合作，包括推动中美两国省―州、城市―城市之间建立合作伙伴关系等。<br />
<br />
　　二、促进互利共赢的贸易和投资<br />
<br />
　　双方认识到开放的贸易和投资环境对两国促进经济增长、创造就业和创新的重要意义，双方致力于构建更加开放的全球贸易和投资体系，反对贸易和投资保护主义。双方同意，在发起贸易救济调查和实施贸易救济措施时严格遵守世界贸易组织规则以防止其滥用非常重要。双方愿共同努力，以包括谈判模式在内的已有成果为基础，确保多哈回合达成平衡、积极的协议。根据近期盘点会的建议，美中双方愿在所有适当的场合和组合中积极进行谈判。两国均承认多哈回合发展目标的重要性。<br />
<br />
　　基于第三次中美战略经济对话的成果，中美重申双方的创新政策将与以下原则保持一致：非歧视；支持市场竞争与开放的国际贸易和投资；强有力的知识产权执法；以及与ＷＴＯ原则保持一致。允许企业之间商定技术转让、生产过程和其他专有信息的方式和条件。中美双方同意尽早于今年夏天在中美科技联委会框架下就创新政策展开深入的专家和高层讨论。这些会议将包括中美双方所有相关部门。在制定和执行创新政策时，中美两国将充分考虑这些讨论的结果。中方承诺在世界贸易组织政府采购委员会7月会议前，提交一份强有力的政府采购协议修改出价，美方对此表示欢迎和期待。美方对中国就政府采购法关于“本国产品”的认定标准草案公开征求意见表示欢迎。<br />
<br />
　　美方将在贸易救济调查中，认真考虑并给予提出“市场导向行业”申请的中国企业公正、合理的待遇，并通过中美商贸联委会以一种合作的方式迅速承认中国市场经济地位。中方表达了对美国技术出口管制的关注。中美双方承诺遵守首轮中美经济对话达成的共识，在《中国―美国高技术与战略贸易发展指导原则》下，采取有效措施，积极落实《中美高技术贸易重点领域合作行动计划》，包括美国政府评估中国政府就出口管制问题提出的关注。<br />
<br />
　　双方重申继续推进双边投资协定谈判。双方同意，成功的谈判将通过促进和保护投资，为双方投资者增强透明度和可预见性，支持双方对开放全球经济的承诺。双方致力于提高外商投资规定的透明度。美方重申，将根据法律规定，在《联邦纪事》上公布美国外国投资审查委员会规程的修改建议，最终发布前的公众评论期不少于30天。中方确认，将根据国务院通知，在国务院法制办中国政府法制信息网上公布《外商投资产业指导目录》修改建议，最终发布前的公众评论期不少于30天。<br />
<br />
　　中方在首轮中美战略与经济对话后已采取行动简化外商投资审批程序，美方对此感到鼓舞。在此基础上，中方承诺进一步简化审批程序，缩短审批时间，提高透明度，缩小审批和核准的范围。美方确认，美国外国投资审查委员会有关程序遵循透明和严格的法定时限，并承诺遵守这些规定。美方将继续努力，澄清包括关键定义和概念在内的外国投资审查委员会的有关程序。<br />
<br />
　　根据经济形势，结合国家产业调整和振兴规划，中国将修订《外商投资产业指导目录》，鼓励外资参与高端制造业、高新技术产业、现代服务业、新能源、节能和环保产业，并扩大这些产业向外资开放的领域。美方欢迎包括中国企业在内的外国投资。美方确认，美外国投资审查委员会的程序确保为所有外国投资，无论其来源地，提供一致和公平的待遇。<br />
<br />
　　中方将继续遵守主权财富基金普遍接受的原则和做法。美方重申将按照经合组织2008年6月公布的《主权财富基金与接受国政策宣言》对包括主权投资基金在内的外国投资者坚持公开和非歧视原则的承诺。<br />
<br />
　　美方认识到许多中国国有企业（国家出资企业）已实行股份制并建立了公司治理结构，实现了股权多元化，其中，许多已成为上市公司。中国继续努力使越来越多的国有企业成为以市场为导向的法人实体。中方将继续推进国有企业改革，进一步推动投资主体多元化，包括发行可公开交易的股票，以及引入包括非公共及外国投资者在内的战略投资者并让其持股。<br />
<br />
　　经济对话结束之后，中美两国进出口银行同意建立贸易融资合作机制，并注意到两行认真落实了此前有关200亿美元贸易融资便利的承诺。此外，中美两国进出口银行还将在联合融资方面探讨合作，采取措施促进中小企业出口，并鼓励参与多边论坛以促进贸易融资领域的合作与交流。中美双方承诺就解决美国大豆输华的检验检疫及质量安全监管问题加强交流，以在第三轮中美战略与经济对话之前签署一份谅解备忘录。中国海关总署和美国贸易代表办公室商定，将就有关原产地问题的具体产品案例研究结果进行讨论，并同意在第三轮中美战略与经济对话前再举行一次原产地专家联席对话。<br />
<br />
　　三、金融市场稳定和改革<br />
<br />
　　双方将继续改革各自的监管框架，使金融机构在金融系统整体稳定的环境下支持实体经济，促进创新。中美双方将寻求通过强化监管、提高透明度和完善问责制，推动金融市场服务于居民、商业和生产性投资需求。双方将采取措施在国家和国际层面落实审慎监管标准，促进平等竞争，避免金融市场分割、保护主义和监管套利。双方支持将在全国范围实施更高水平和更高质量的资本要求和反周期的缓冲资本作为巴塞尔协议资本框架的要素，支持提高流动性风险要求和设立前瞻性的拨备，以降低银行承担过度风险的动机，创造一个能抵御负面冲击的更强健的金融体系。双方支持引入一个杠杆率标准作为巴塞尔协议基于风险的框架的补充措施。<br />
<br />
　　双方认识到在全球化背景下，两国金融领域一体化程度日益提高，双方承诺就具有跨境影响的金融监管议题加强合作。中国银监会和美国联邦存款保险公司将加强双方在各自管辖区域内跨境问题银行机构处置方面的监管合作和信息共享。中国人民银行和美国联邦存款保险公司将加强在存款保险、金融服务、倒闭金融机构处置及其他与金融稳定相关领域的合作。双方将进一步就包括保险资产管理在内的保险监管进展加强合作与交流。<br />
<br />
　　美国致力于实施全面的金融监管改革，应对过度冒险，降低道德风险，为消费者提供更多保护，更好解决微观和宏观的审慎监管，解决“过大而不能倒闭”的金融公司的问题。<br />
<br />
　　美方承诺继续加强对“政府支持企业”的监管，确保“政府支持企业”具有足够资本和能力以履行其财务责任。美国政府广泛的住房政策，包括支持“政府支持企业”继续开展业务、美财政部和美联储购买按揭抵押证券，对恢复住房市场稳定性和保持按揭信贷的可获得性一直发挥了十分重要的作用。美国政府将听取所有利益相关者的意见并寻求与国会密切合作，以制定一项全面的“政府支持企业”改革方案。美国政府承诺采取合适的方式进一步推进改革，确保“政府支持企业”具备履行偿债义务的能力。<br />
<br />
　　中方已启动股指期货交易，将允许在中国境内依法设立的合格外商投资公司依照相关法律法规开展股指期货业务。中方将在审慎监管的基础上，允许合格境外机构投资者投资股指期货产品。<br />
<br />
　　美方欢迎中方根据国际标准在改善国际收支及官方储备数据报告的准确性和频率方面取得的进展。中方表示将根据国内实际情况和统计能力继续改进这方面工作。<br />
<br />
　　四、 国际金融体系改革<br />
<br />
　　双方重申支持Ｇ20作为国际经济合作主要论坛，在国际经济和金融事务中发挥更大作用。双方同意共同努力，确保多伦多、首尔峰会取得积极成果。<br />
<br />
　　双方同意，国际货币基金组织（ＩＭＦ）必须在促进全球金融稳定和增长再平衡方面发挥关键作用。双方重申支持落实领导人在匹兹堡峰会上关于国际货币基金组织份额和治理结构改革的承诺，欢迎去年Ｇ20成员通过新借款安排为国际货币基金组织成功增资5000亿美元，并同意共同努力改革国际货币基金组织，使其更好履行其义务。<br />
<br />
　　双方欢迎多边开发银行为减轻全球金融危机对世界最贫困群体影响所采取的关键行动，重申支持多边开发银行在全球减贫中的中心作用。双方承诺共同努力，通过普遍增资确保多边开发银行有足够资源。双方欢迎近期达成的支持世界银行、泛美开发银行和非洲开发银行增资并承诺进行强有力改革的原则共识。双方还欢迎就世界银行新的股权安排达成的共识，这将更好地反映发展中和转轨国家在世界经济中的分量，同时保护最小穷国的发言权。双方还期待共同努力推动多边开发银行优惠贷款窗口的成功增资，这些机制对最贫穷国家至关重要。<br />
<br />
　　为了应对需要全球采取协调行动的全球性挑战，双方同意，世界银行与地区开发银行及其他国际组织应加强对以下问题的关注：通过提升农业生产力以及获取技术和食物以确保粮食安全；最贫困和最具挑战性环境中的人类发展问题；私营部门拉动的增长和基础设施；有效回应有关国家将气候变化与经济发展结合的需求。<br />
<br />
　　双方同意进一步加强在金融稳定理事会（ＦＳＢ）和全球税收透明度与情报交换论坛的合作。鉴于中国和中国香港特别行政区当前的经济规模、金融市场活动和国家金融稳定安排，美方支持中国和中国香港特别行政区保持在金融稳定理事会的现有席位。作为金融稳定理事会成员，中美双方致力于保持金融稳定，保持金融业开放，提高金融业透明度，执行Ｇ20领导人同意的关键的国际金融标准，并承诺进行定期的同行评估。<br />
<br />
　　（本报北京5月25日电）</div>

							
							
							
							
