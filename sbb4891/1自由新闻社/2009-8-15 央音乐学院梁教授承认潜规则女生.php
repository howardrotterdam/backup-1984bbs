													<h2>央音乐学院梁教授承认潜规则女生</h2>
						
						
																											<div id="postmessage_203893" class="t_msgfont">中央音乐学院七旬博导主动坦白曾潜规则女生<br />
<br />
<a href="http://news.QQ.com" target="_blank">http://news.QQ.com</a>　 2009年08月15日03:01 　 新京报　 　 我要评论(184) <br />
<br />
本报讯 中央音乐学院一位70岁知名博士生导师、教授自曝曾与一名准备考取该校博士研究生的女学生，发生肉体关系，并收受该生10万元贿赂，以便帮助其顺利考博。最终因女学生并未如愿上博，老教授向校方纪检部门坦白此事。<br />
<br />
<br />
昨日，中央音乐学院发言人证实此事，称已对当事教授作出严肃处理。<br />
<br />
博导携妻子到校纪检坦白<br />
<br />
该教授姓梁，为中央音乐学院的博士生导师，长期从事中国近现代音乐史的教学研究，在音乐评论界很知名，还曾在《百家讲坛》讲座。<br />
<br />
“他是主动坦白的。”昨日，中央音乐学院何姓发言人称，今年7月份，该教授携妻子一起来到学院纪检部门称，与一名邹姓女学生发生超越师生的肉体关系，收受该生10万元贿赂。<br />
<br />
梁教授称，邹姓女生是外地学校硕士研究生，今年准备考取中央音乐学院的博士生。为能如愿被录取，邹先给梁5万元作为“学费”。此后又给梁5万作为疏通关系的“打点费”。<br />
<br />
今年5月份，中央音乐学院的博士生录取成绩公布，邹姓女生并未如愿考上博士。知情人透露，此后邹找梁闹，甚至到学校相关部门讨说法，“梁觉得很被动，所以主动向校方坦白。”<br />
<br />
何姓发言人坦言，梁向校方承认问题态度比较诚恳，这位70岁的高级知识分子可用“声泪俱下”形容。向校方坦白前，梁已将10万元退给邹姓女生。<br />
<br />
校方取消该博导三项权利<br />
<br />
何姓发言人称，事情发生后，学院召开了主要负责人会议，决定对其“取消所有招生、教学和学术的权利”。对此，何姓发言人解释，梁是该校退休人员，由于梁专业突出而被返聘代课和招生，所以不存在开除的问题。这种“取消”是学校范围内的，永久性的。至于梁个人在社会上的学术等权利，学校也无权干涉。<br />
<br />
因为邹姓女生并不是中央音乐学院的学生，校方未对其进行处罚。<br />
<br />
另据了解，学院对邹姓女生考博的各项成绩进行调查，发现除了梁教授打分“过高”，其他参加考核的4名教授打出的分还是公正、客观的。该生最终仍未能录取。<br />
<br />
中央音乐学院发言人表示，这是建校以来首次出现的情况。他代表校方发出呼吁，广大考生千万不要相信个别教授、中介等对考试做出的承诺。<br />
<br />
昨日，国家大剧院人员表示，原定“八月合唱节”中给公众讲座的梁教授，目前已经被取消讲座。知情人透露，原因可能与梁出事有关。<br />
<br />
学生印象<br />
<br />
学生惊呼“不能相信”<br />
<br />
“不能相信！不敢想象！”昨日，中央音乐学院的多名学生表示。在这些学生眼中，梁教授是一名德高望重，值得尊敬的老师。<br />
<br />
上过梁教授课的学生称，梁是音乐系最好的老师之一，他的课都是“满员”状态。<br />
<br />
一名研究生说，梁平时对学生特别好，课余时间学生向他请教问题，他都会耐心解答。学生们要做论文，梁也会帮着查找资料。<br />
<br />
昨日，一名中央音乐学院的教师坦言，近年来梁教授知名度越来越高，出了这事一下子名声扫地，“我们都为他感到遗憾。”<br />
<br />
专家说法<br />
<br />
教师招生收重金涉嫌受贿罪<br />
<br />
中国人民大学法学院教授王宗玉认为，教师、教授在招生过程中，利用手中职权收受重金贿赂涉嫌受贿罪。<br />
<br />
“两高”发布《关于办理商业贿赂刑事案件适用法律若干问题的意见》的司法解释，规定医务人员、教师“涉贿”数额较大的，以非国家工作人员受贿罪定罪处罚。<br />
<br />
王宗玉认为，梁虽是退休返聘，在招生、判卷过程中手中仍有职权，可以构成涉嫌受贿罪主体。<br />
<br />
相关新闻<br />
<br />
大学老师收钱被诉涉嫌受贿<br />
<br />
本报讯（记者 田北北）因收受学生贿赂，作为某大学学生工作部部长（正处级）的许某，日前被朝阳检察院以涉嫌受贿罪提起公诉。据了解，许某于2005年1月至6月间，利用负责学生就业工作的职务便利，以为学生推荐工作和办理留京户口为由，先后分三次收受学生李某的贿赂共计7.3万元。<br />
<br />
本组稿件采写（除署名外）/本报记者 耿小勇 张宁</div>

							
							
							
							
