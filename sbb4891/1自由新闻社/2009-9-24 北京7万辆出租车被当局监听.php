人流众多的西单地区街道及不少大厦楼顶，亦加装了大量闭路电视。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 距离国庆只有7天，北京保安措施将达到来有史以来的最高级别：北京公共场所将没有个人空间和私隐，连被认为属私人空间之的士车厢，都悄然安装了微型监听器。市民和游客在不知情下，其私人谈话可能已被有关部门监听。据悉，这项政策去年奥运期间曾局部实行，现已扩至国庆期间全市实行，多数的士乘客都认为这是「侵犯了人权」。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 此外，北京的客流预警系统前日在西单首次运作，公开理由是「防止发生人群践踏」，但实际是监控人群聚集，以防突发事件。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 车内装GPS连咪 接通公安局<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 北京的士司机以爱好谈论政治而闻名全国，也是内地小道消息的重要来源。不过，记者近期在北京乘搭的士时发现，随着国庆临近，爱聊天的北京的士司机却日趋沉默，即使熟络的司机，也以「莫谈国事」来避谈政治。记者经调查得知，原来是装在车厢顶的两个「小东西」作怪。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 目前，北京每辆的士车内都装有微型监听咪，它与一个能透过卫星跟踪的士方位的GPS全球定位系统相连，直通北京市公安局情报信息中心。这些原本用来保护司机安全的微型咪还有用途，便是用来远距离监听的士车内的谈话。北京的士司机之间曾流传一个小笑话：一个司机开车行走长安街时遇到交通管制塞车，他因此向搭客发牢骚，大骂领导人好大喜功，劳民伤财，结果开了不到10分钟还未驶出长安街，已被公安截停，要他「交代问题」。据说这是北京警方的功能测试，这名倒楣的司机成了测试成功的第一个牺牲品。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 市民游客多不知情：太过分<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 本报记者昨在北京多个中心城区调查发现，大部分北京市民和游客不知道的士车厢安装监听器，但普遍持反对态度。在国贸地铁站附近，一对刚下的士的年轻情侣听到记者提问后惊讶地说，「难道我们谈话都被有关部门偷听到了？这也太过分了！」<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 虽然北京保安专家表示，有关部门不太可能监听车中所有对话，但北京多数司机和乘客还是对这类装置表示担忧，一些的士业者私下表示，奥运期间，部分的士安装微型监听咪曾引起国际风波，经过一年试验评估，这一做法，在今年国庆期间，将扩展至全市所有7万辆的士。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 记者向北京市公安局查询，警方是否监听的士车厢里的对话，当值警员拒绝置评，指这类事情属保密，按规定不能向公众透露细节。北京多家的士公司职员则向记者强调，安装微型咪是为了保护司机的人身安全。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 此外，有消息称，北京警方日前招募逾万的士司机当「国庆治安情报员」，如能提供可靠线索导致破案，最高可获50万元人民币奖励。警方选择的士司机是任「情报员」，因为他们活动范围广泛，易遇到各色人等。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 另据《新京报》报道，北京首个客流预警系统「西单客流预警系统」前日启用，将对西单地区天桥、商场、地铁站、公交站4类场所进行人群密度测试，防范发生人踩人事故。西城区有关部门负责人称，监控系统目前设有19个资料获取点，其中8个集中在西单路口附近，10．1后将向西单北大街扩展，届时将达40个。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 较早前，官方传媒报道，北京地铁内的闭路电视将升级增加录音功能。根据官方规定，地铁内的视频监控系统须加装「音频采集器」，一旦遇到突发事件，警方调取资料后，可还原现场「原音」。<br />
<br />
――――――――――――――――――――――――――――――――<br />
<br />
北京的士执行监听有损形象应该叫停 <br />
<br />
【明报专讯】下周四就是中华人民共和国成立60周年的日子，鉴于国内外情?复杂，全国（特别是京畿重地的北京城）为确保“平安国庆”，都提高警戒级别，采取的保安措施，可以理解。不过，北京除了在地铁监控民?，还在的士内装置监听设备，监听乘客的说话，此举大有值得商榷之处，不但侵犯个人私隐，更强化中国“大阿哥“无处不在地监控人民的负面形象。 <br />
<br />
