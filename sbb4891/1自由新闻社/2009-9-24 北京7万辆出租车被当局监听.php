<br />
国庆临近。近年，取态激进的藏独和疆独分子，多会选择一些重要日子制造事端和搞破坏，以吸引国际间的注意，例如去年北京奥运，藏独在国外，疆独在国内都曾搞事，企图破坏京奥的氛围。在这样的背景下，加强国庆保安，警方在全国展示具震慑作用的实力，可以理解。 <br />
<br />
北京的保安更是空前森严，动用了军队，警察，武警，保安和志愿者五大力量，反恐精锐之师的雪豹突击队，身怀绝技的蓝剑突击队将延续奥运的特殊使命，承担反恐任务。北京警方按照“逢车必检”，“逢人必查”，“逢疑必录”的原则，在200多个进京主要路口和治安检查站，对进京所有车辆实行24小时安检，逐一核查进京人员身分证件，一旦发现疑点立即处理。市内主要街道路口，都有全副武装的武警站岗，北京当局还动员140万名志愿者，组织“群防群治队伍”，在北京的大街小巷站岗，作为民间的“哨兵”参与治安保障工作。 <br />
<br />
这样的严密高度警戒，把北京打造成水泼不进的铁桶，对于震慑恐怖分子，犯罪集团，肯定起到作用。但是当局就重要日子的大部署，还有其他目的，就是压抑民?表达对当局的不满，以创造表面和谐的社会氛围。近年，每逢重要日子，北京所采取维稳手段愈趋高调而强力，致使上访者多被堵在北京城外，地方政府和官员一些胡作非为所导致的大量民怨，无从宣泄。因此，北京的铁桶保安，若为杜防恐怖分了破坏，可以理解和接受，若为压倒对民生事务不满却求助无门的民?，则是使人遗憾了。 <br />
<br />
