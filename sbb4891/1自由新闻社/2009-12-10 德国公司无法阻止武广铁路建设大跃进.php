													<h2>德国公司无法阻止武广铁路建设大跃进</h2>
						
						
																											<div id="postmessage_365482" class="t_msgfont">武广专线穿越鄂湘粤三省的河流、丘陵、崇山峻岭，地形复杂，其中遇到哪些技术上的困难，有哪些突破？本报记者专访了铁四院武广项目副总工程师曾小林。<br />
<br />
&nbsp; &nbsp; 记者(下简称“记”)：武广运行时速350公里，在世界范围内看是什么水平？<br />
<br />
&nbsp; &nbsp; 曾小林(下简称“曾”)：德国一般时速300公里，日本一般跑270公里。当然对运行速度也有不同理解，譬如日本也能时速到300公里以上，但是他们研究认为，时速270公里以上能耗比较高，就是说你节省的时间创造的价值不能抵消所需的能耗。但是我们国家疆域辽阔，需要快。<br />
<br />
&nbsp; &nbsp; 记：跟其他高铁比，武广有何特色？<br />
<br />
&nbsp; &nbsp; 曾：第一，建设速度快。建成只用了4年半时间。德国科隆到法兰克福几百公里高铁建设用了七八年。第二，有些工程技术世界领先。湖北咸宁境内有座汀泗桥特大桥，要跨越4车道的京珠高速，还要预留其扩建成8车道空间，在140米大跨度桥梁上铺设无砟轨道，这是世界领先。国外无砟轨道最大跨度是80米。第三，大断面的隧道。武广专线桥隧占到67%。浏阳河隧道、大瑶山隧道都有十余公里长。第四，三省地质复杂，不少地方是喀斯特地质，要实现地基零沉降，非常不容易。高铁路基最重要的就是要控制沉降。最开始，负责设计咨询的德国欧普曼公司建议路基观察两年再做，但是我们的工期不允许。<br />
<br />
&nbsp; &nbsp; 记：不放一段时间观察也不行吧？<br />
<br />
&nbsp; &nbsp; 曾：我们也会观察一段时间。观察半年后没问题再铺设无砟轨道。<br />
<br />
&nbsp; &nbsp; 记：武广全线铺的是无砟轨道，为什么要无砟？<br />
<br />
&nbsp; &nbsp; 曾：噪音小，污染小，没有现在火车的哐当声。我国在建的9条客运专线中，只有武广、京津、郑西三条是无砟轨道。<br />
<br />
&nbsp; &nbsp; 记：勘察设计时，遇到过哪些困难？<br />
<br />
&nbsp; &nbsp; 曾：2004年铁四院中标武广高铁，当时最大困难是技术标准的确定，2005年6月，武广高铁在长沙开工时，无砟轨道标准还没完全确定，又不能照搬国外标准。因我国是轨道电路，法国用的是有砟轨道，而德国不是轨道电路。2004年至今，我们确立了中国高铁标准。铁道部准备把中国高铁标准翻译成英文，相信在国际上也非常有市场。<br />
<br />
&nbsp; &nbsp; 记：有无技术之外的困难？<br />
<br />
&nbsp; &nbsp; 曾：跟沿线政府的协调。铁路跟河流、高速公路、西气东输工程等很多地方有交叉，不少地方经历了艰难协调。大家都想对自己有利。我们从技术角度谈，但是地方政府要从投资角度谈。<br />
<br />
&nbsp; &nbsp; 采写：武广专线报道联盟本报记者 周文峰　潇湘晨报记者 谢功梅 通讯员刘新红（除署名外）</div>

							
							
							
							
