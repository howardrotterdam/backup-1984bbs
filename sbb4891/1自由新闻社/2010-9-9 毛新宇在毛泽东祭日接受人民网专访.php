毛新宇分析到，土城之战，造成毛泽东的失误，是什么原因呢？是情报侦察有失误。因为土城之战，当时毛泽东指挥的时候，他了解的时候，说川军一个部没有多少兵力，说不足一个团。毛泽东为什么后来这么重视情报工作？他在实际指挥土城战役的时候非常恼火，他为什么恼火？恼火就恼火在情报失误上。有时候情报失误对战场判断是害死人的。<br />
<br />
“比如人家报告说郭勋祺的军队只有两个营，但是毛泽东的实际指挥一打，郭勋祺不仅能打，他的兵力比两个营大得多，是两个团。情报不准，怎么办。毛泽东即使在失误的情况下，毛泽东最可贵的是能够根据战场的实际情况马上转变策略，当机立断。毛泽东为什么说，他有四渡赤水，他的得意之笔，有的时候他抉择失误了他调整战略有密切的关系。按照原来，毛泽东打赢郭勋祺就可以从这儿北渡长江到四川，有可能就不用四渡赤水，就是因为当时川军郭勋祺特别厉害能打，所以毛泽东及时调整，当然也是为了摆脱敌人参与四渡赤水。”毛新宇说。<br />
<br />
毛新宇说：“我再说一个，再点一个，算是毛泽东指挥战争的失误。比如，今年我们纪念伟大的抗美援朝战争爆发60周年，我在学习了解抗美援朝过程中，有这么一个历史，这个历史是什么呢？抗美援朝的时候，我们军事科学院我的一个老领导（宋时轮――编辑注），当时他是第九兵团司令，第九兵团入朝作战，我们不能说是失误，但是它打这一仗，第九兵团损失比较大，什么原因呢？那是因为当时志愿军入朝的时候，有些事情、问题没有想到，就是朝鲜的气候变化，非常寒冷。毛主席也是下了很大决心，而且要求志愿军参战都很急，当时没有考虑到朝鲜的天气会变得很严寒，很寒冷。我们的志愿军也是非常苦，主要是在抵御寒冷、抵御自然界，冬装这些方面没有准备充足，非战斗减员，冻伤的或者是冻死。当然，有些就说明情况是意想不到的。 ”<br />
<br />
毛新宇最后说，毛泽东确实也不是常胜将军，世上没有常胜将军。有一句话说得最经典，毛泽东说“我这个人就是靠总结经验吃饭的”。失败乃成功之母，没有失败，不去总结失败的经验教训怎么会成功呢？</div>

							
							
							
							
