													<h2>毛新宇：毛泽东陶毅相恋失实</h2>
						
						
																											<div id="postmessage_822753" class="t_msgfont">　中新网9月9日电 9日下午，毛泽东唯一嫡孙，毛泽东思想研究专家、军事科学院战争理论和战略研究部副部长毛新宇少将做客人民网，回答网友和媒体提问。<br />
　　“毛泽东恋人陶毅”并非历史事实<br />
　　有网友问到，《建党伟业》中毛泽东有个叫陶毅的恋人，是不是事实？<br />
　　毛新宇说，这并非是真实的历史事实。“因为经过我对党史的研究，包括对我爷爷个人生平的了解，毛泽东在建党前后，除了在第一师范求学，甚至包括他1918年在北京大学工作期间，讲话杨昌济推荐，到李大钊手上工作，一直到中国共产党成立，我认为毛泽东最爱的伴侣和妻子始终是我的奶奶杨开慧，这仅是从我爷爷个人的思想感情上面来说。”<br />
　　毛新宇补充说，在1920年12月，爷爷毛泽东和奶奶杨开慧在长沙第一师范附小主事室(校长室)结为夫妻。“由于这个基本历史事实，所以我的奶奶杨开慧是建党以后第一批女共产党员(杨开慧是1921年加入共产党的)，既然说我的爷爷奶奶是1920年12月结婚的，共产党成立是1921年7月，所以，在这个历史时期，我爷爷所爱的，而且他的合法妻子只可能是杨开慧一人，而不会是其他人。”9日下午，毛泽东思想研究专家、军事科学院战争理论和战略研究部副部长毛新宇少将做客人民网时说，当少将以后，在个人的工作水平、知识结构方面，在政治素质、思想政治方面，要更加地严格要求自己。<br />
　　中国第一艘航母是否以毛泽东命名不重要<br />
　　有媒体问到，您在09年提过一个提案，建议中国第一艘航母以毛泽东命名，不知道有关部门有何答复？<br />
<br />
有媒体问到，您在09年提过一个提案，建议中国第一艘航母以毛泽东命名，不知道有关部门有何答复？<br />
　　毛新宇说，首先纠正，这仅仅是我的一个个人想法，不是提案。他说，但是我现在想阐述的是，由于我作为将军，我刚才提到了，我特别关心我们的二炮部队和海军的发展建设，尤其是本人非常关心海军的航空母舰的兵种的建设问题。他表示，“我和全国人民一样，很希望我们的海军拥有自己的航母舰队，这涉及到我们国家的尊严问题，也涉及到维护我们国家主权的问题。只要我们的人民海军拥有了航空母舰，具体如何命名那并不重要，最关键的问题就是我们国家一定要拥有自己的航母舰队。”<br />
　　赞妻子刘滨是优秀军人<br />
　　毛新宇在回答网友的提问时说，妻子刘滨也是一位合格而又优秀的女军人。“她已经在部队工作16年，她在我的学习和生活当中给予我很多的帮助，应该是我的一个好助手。同时她本人现在工作负担很重，还要替我承担和管理很多家庭事务。对此，我对妻子表示感谢。”<br />
<br />
9月9日是毛泽东逝世34周年纪念日。当日上午，毛泽东的孙子、刚刚晋升为解放军少将的毛新宇与夫人刘滨，带着两岁的女儿毛甜懿到位于北京天安门广场的毛主席纪念堂敬献鲜花。<br />
　　当少将以后给自己提出了两条重要要求<br />
　　毛新宇说，今年晋升为少将以后，除了个人有荣誉感以外，也感觉到了自己肩上的压力。当少将以后，给自己提出了两条重要要求。首先，要使自己做到作为一个优秀的军人，在做优秀军人的同时还要做好优秀的将领、优秀的将军。<br />
　　毛新宇认为，不论是做优秀军人，还是做一名合格的将军，自己所需要学习的知识还很多很多。不仅要学习丰富的各种科学理论知识，还要经常去接触实际、接触基层，还要及时地掌握国内、国际上的军事变革信息和动态。<br />
　　“作为一个军人，作为一个少将是很不容易的。同时，我觉得除了在个人的工作水平、知识结构方面，在政治素质、思想政治方面，我也要更加地严格要求自己。 ”毛新宇说。<br />
　　自己对于现代信息化战争的了解还非常欠缺<br />
　　毛新宇谈到，自己对于现代信息化战争的了解还非常欠缺、非常少，今后，要对现代信息化战争的各种知识加强学习和了解。<br />
　　他说，作为一个军人，现在非常关心国家的国防建设，特别是二炮部队和海军、空军方面的建设。对当年爷爷制定“两弹一星”的发展战略以及我军“杀手锏”――二炮部队的成长发展历史是非常感兴趣的。<br />
　　同时，毛新宇还注重研究毛泽东的情报战略思想，希望能够通过学习和研究，拓宽知识领域，能够加深对信息化战争的了解和学习，只有这样才能有资格地成为一名合格的人民解放军将领。 同时，还愿意深入到基层部队，特别是空军、海军、二炮，向一切现在各军兵种取得优异成绩的，而且是具有实战经验的优秀年轻的指挥员们学习。<br />
　　伯父毛岸英是家族的骄傲<br />
　　毛新宇说，对伯父的了解最主要、最直接的是通过父母。小时候，父亲毛岸青主要讲了他的两段最重要的人生经历，一个就是上海流浪，这个大家都知道的，上海流浪也是他们三兄弟的上海流浪。在流浪过程中岸龙叔叔得病，很早就去世了。<br />
　　毛新宇说：“我伯父活着的时候，他有一次带着新婚的妻子到电影院看《三毛流浪记》，当时他很激动，就拉着我大姨的手说，我们三兄弟在上海的流浪生活，除了没有给资本家当干儿子以外，其他过的悲惨生活和三毛是一模一样的。”<br />
<br />
　毛新宇说，父亲生前多次讲过他和伯父在苏联的留学生活情况，也提到伯父参加苏联红军，和苏联人民一起抗击法西斯德军的故事。<br />
　　“除了我父亲给我讲伯父的故事以外，还有就是我的外婆。”毛新宇说：“张文秋老人是一个革命老人，是一个老共产党员，1926年入党，是我的外婆。我和我外婆生活的时间比较长。20多年，平时我在课余的时候，她对我进行革命传统教育，多次讲到我的伯父，因为我外婆确实很喜欢她这个女婿。 ”<br />
　　毛新宇说，母亲也让他对伯父有所了解。“那是1986年，在我16岁那年，在我没有思想准备的情况下，我妈妈让我跟随青年代表团，我人生第一次出国就是去的友好邻邦――朝鲜，而且我还见到了朝鲜人民的领袖，见到了金日成。通过给我伯父的第一次扫墓，就是1986年第一次扫墓，毛岸英的业绩和高大光辉的形象就深深地扎根在我的心里。”<br />
　　曾给伯父写了一首歌和一部书<br />
　　毛新宇说，后来，自己满怀深情写了一部书叫《我的伯父毛岸英》。1990年，又和母亲给伯父扫墓，2000年的时候，母亲在军委的批准下成立了一个毛岸英纪录片摄制组，跟着母亲到了朝鲜。那一年是纪念抗美援朝50周年，很隆重，自己还给伯父写了一首歌，叫《无限怀念在心中》。2006年，在志愿军入朝55周年的时候，那一次也是很难得的，中央批准的，母亲带队，所有的毛家的亲属专门组织的代表团，这是自己第四次去访朝，去给伯父扫墓。那一次家里的亲属去的都比较全。最近的一次就是今年5月份，中央军委高度重视的，抗美援朝志愿军烈属代表团。访朝五次，给伯父扫了五次墓。 ”<br />
　　毛新宇说：“另外，我亲身沿着伯父的足迹到了他生活、战斗的地方，比如我到沿岸，我亲自到了毛岸英上的劳动大学，当时我亲自看到伯父的劳动大学，他当时生活居住的条件之艰苦，而他当时学作农活的勤奋和努力，都给了我深刻的印象。……另外毛岸英搞土改的一个地方，叫山东的渤海老区，我也去了，亲自看到了毛岸英土改工作队，在山东渤海当年住的房子。 ”<br />
　　毛新宇说：“我通过访朝，通过父母、外婆给我的讲述，而且我又到了伯父战争、生活过的地方，所以我比较全面地了解了毛岸英生平，所以我觉得他是我们家族的骄傲，我无比地崇敬他。”<br />
　　毛泽东的军事思想和经济思想是一体的<br />
　　在回答网友的提问时谈到毛泽东的经济思想。毛新宇回答到，毛泽东思想是一个博大精深的科学体系。历史已经做出了科学的回答，毛泽东是一个伟大的经济学家和伟大的经济战略学家。在革命战争时期，毛主席在新民主主义革命过程中就提出过革命根据地的建设，要建设革命的根据地，建立巩固的革命根据地是毛泽东思想的重要原则和重要内容。要想建立一个巩固的革命根据地，不发展经济是做不到的。同时，毛泽东指出，毛泽东曾经就论述过，革命战争和经济的关系，革命战争一个要看战争的正义性和非正义性，一个看人民群众拥不拥护战争，但是战争绝对离不开经济，没有经济、物质的支持，即使是人民战争，即使你是正义的，也很难维持下去。所以，毛泽东在他领导中国人民进行民主革命战争的时候，他就有很丰富的经济学思想，这就包括了毛泽东的革命根据地的经济建设，怎么样发展我们的经济支援伟大的人民战争理论。所以，毛泽东的军事思想和毛泽东的经济思想是一体的，是不可分割的。<br />
