													<h2>广电总局的“十类禁止九类应删”</h2>
						
						
																											<div id="postmessage_610119" class="t_msgfont">《无人区》黄了 / 江涛NO1<br />
<a href="http://www.my1510.cn/article.php?id=87ec963c438de364" target="_blank">http://www.my1510.cn/article.php?id=87ec963c438de364</a><br />
<br />
宁浩新作《无人区》再次“黄了”，上映日期无限推迟。广电总局电影审查委员会老同志赵葆华说“影片里没有英雄净是坏蛋，违背生活真实与艺术真实。”由于广电总局审片委员会历来作风神秘，能够确切透露“干掉”一部片子的理由，也算难能可贵。对于这样的理由，暂不论能不能站得住脚。先来看“审片委员会”是一个什么样的机构，它的成员来自何方，它自身又是如何运作的。<br />
<br />
审片委员会隶属国家广电总局，执掌着中国大陆上映的所有电影的生杀大权。也就是说，一部电影只有通过审片委员会的审查，被认为适合播放，才会投放院线。如果被认为不合格、不适宜播放的电影，则被驳回修改、重新剪辑，或者直接被枪毙。<br />
<br />
据内部人士透露，审片委员会成员仅36人，背景来自各行各业，如北京电影学院导演系教授、老牌电影导演、全国妇联宣传部文化处处长、大学教授、国家宗教事务局办公厅综合信息处处长等。这36人审核电影能否上映的标准主要涉及意识形态与官方政策基调。具体的标准则是广电总局的“十类禁止九类应删”，罗列如下：<br />
<br />
<font color="blue"><strong>十类禁止</strong> <br />
<br />
（一） 反对宪法确定的基本原则的；<br />
<br />
（二） 危害国家统一、主权和领土完整的；<br />
<br />
（三） 泄露国家秘密、危害国家安全或者损害国家荣誉和利益的；<br />
<br />
（四） 煽动民族仇恨、民族歧视，破坏民族团结，或者侵害民族风俗、习惯的；<br />
<br />
（五） 宣扬邪教、迷信的；<br />
<br />
（六） 扰乱社会秩序，破坏社会稳定的；<br />
<br />
（七） 宣扬淫秽、赌博、暴力或者教唆犯罪的；<br />
<br />
（八） 侮辱或者诽谤他人，侵害他人合法权益的；<br />
<br />
（九） 危害社会公德或者民族优秀文化传统的；<br />
<br />
（十） 有法律、行政法规和国家规定禁止的其他内容的。<br />
<br />
电影技术质量应当符合国家标准。 <br />
<br />
<br />
<strong>九类应删剪</strong><br />
<br />
<br />
<br />
　　(一)曲解中华文明和中国历史，严重违背历史史实；曲解他国历史，不尊重他国文明和风俗习惯；贬损革命领袖、英雄人物、重要历史人物形象；篡改中外名著及名著中重要人物形象的；<br />
<br />
　　(二)恶意贬损人民军队、武装警察、公安和司法形象的；<br />
<br />
　　(三)夹杂淫秽色情和庸俗低级内容，展现淫乱、强奸、卖淫、嫖娼、性行为、性变态、同性恋、自慰等情节及男女性器官等其他隐秘部位；夹杂肮脏低俗的台词、歌曲、背景音乐及声音效果等；<br />
<br />
(四)夹杂凶杀、暴力、恐怖、鬼怪、灵异等内容，颠倒真假、善恶、美丑的价值取向，混淆正义与非正义的基本性质；刻意表现违法犯罪嚣张气焰，具体展示犯罪行为细节，暴露特殊侦查手段；有强烈刺激性的凶杀、血腥、暴力、吸毒、赌博等情节；有虐待俘虏、刑讯逼供罪犯或犯罪嫌疑人等情节；有过度惊吓恐怖的画面、台词、背景音乐及声音效果；<br />
<br />
　　(五)宣扬消极、颓废的人生观、世界观和价值观，刻意渲染、夸大民族愚昧落后或社会阴暗面的；<br />
<br />
　　(六)鼓吹宗教极端主义，挑起各宗教、教派之间，信教与不信教群众之间的矛盾和冲突，伤害群众感情的；<br />
<br />
　　(七)宣扬破坏生态环境，虐待动物，捕杀、食用国家保护类动物的；<br />
<br />
　　(八)过分表现酗酒、吸烟及其他陋习的；<br />
<br />
　　(九)有违相关法律、法规精神的。</font><br />
<br />
 <br />
如果看得足够仔细，你会发现，十类禁止标准，只有一条针对影片的技术和质量问题，其它全部是针对影片的思想和内容的。至于九类应删减的，范围之广几乎涉及电影的方方面面，从拍摄内容、对象，到拍摄手法、影片价值观，甚至音乐、台词均会管到。“十类禁止九类应删”与其说是明确的审片标准，不如说是一个模糊的纲领性文件，具体执行完全看个人发挥。这便是审查制度的荒诞之处，即没有人知道应该按什么标准来审查。因此，如果一部电影需要被禁播，理由总是会找到的，而且很充分。<br />
<br />
稍微列举一下90年代未通过审查的片子，包括《十七岁的单车》（2001）《鬼子来了》(2000年)，《苏州河》(2000年)，《过年回家》(1999年)，《东宫、西宫》(1996年)，　《活着》(1994年)，《蓝风筝》(1993年)，《北京杂种》(1993年) 等。<br />
<br />
 再来看未通过审查的原因，部分如下：<br />
<br />
《十七岁的单车》被禁理由是“如果看过影片就会觉得这部作品没有什么实际意义，表现的就是年轻人对现实的看法和生活经历，基调是灰色的，立意上也有问题。”<br />
<br />
《鬼子来了》被禁理由是“影片一方面不仅没有表现出在抗日战争大背景下，中国百姓对侵略者的仇恨和反抗，反而突出展示和集中夸大了其愚昧、麻木、奴性的一面。”<br />
<br />
比较有意思的还有贾樟柯讲述小人物命运的三部曲，《站台》、《小武》、《任逍遥》，均未通过审查。<br />
<br />
 <br />
如果你有足够的耐心，整理出所有被审片会禁止的影片就会发现，整个90年代中国最优秀的电影，几乎全部未通过审查。这样的审片委员会，其工作的标准和价值衡量方式就很让人质疑。制片人方励曾在接受采访时无奈的说，审片委员会成员都是中国老一辈的电影艺术家，他们普遍年事已高，在观念和审美上都与当今的观众和年轻人的价值观有很大的距离……导演是年轻的导演，目标的观众也是年轻的观众，结果一帮老头老太太来审这个电影，让老人家来决定年轻观众应该看什么样的电影。<br />
<br />
对于这样的审查团队，你会觉得不可思议么。但它的确有自己的工作逻辑，那就是严格维护“十类禁止九类应删”，对于能删的尽量删，对于不怎么对味的尽量禁。对于色彩黯淡、不积极向上的尽量改，对不符合社会主义价值观的要指导改正。36位老人，俨然是中国13亿电影观众、庞大的电影工业与不变的国家利益保护者，他们按照指示条款与阶级斗争时代的审查精神，一板一眼，不慌不忙的完成本职工作。他们也许从来不想观众想看什么，因为他们本身就是观众，并自认能代表中国所有观众的品味。<br />
<br />
 如果你追问他们，艺术品价值如何检查、又怎样判断？那肯定毫无答案。因为艺术价值的判断不是他们的专长，许多时候也不是他们能理解的，就像《站台》，在他们眼里就是一部基调灰暗，价值观颓废的片子，他们丝毫不感到这是一代人的时代生活印记。<br />
<br />
因此，《无人区》被认为“为了艺术极致，不惜违背生活真实和艺术真实……其实这还不是主要的失误，最主要的失误是丢失了艺术家的一份社会责任”，就毫不让人奇怪。他们丝毫没有看到中国导演的创造力，看到一种新的电影风格的成型与成熟，只是考虑到“教育、教育、责任、责任”，导演需要负担什么责任呢？观众又需要多少教育呢？对于一个普通的观众，他花钱进影院，看了一部好看的电影，这还不够么？<br />
<br />
