<br />
随着电影院和银幕数的增加，如今中国大片儿首日过千万已经不是什么新鲜事，不过贱国大孽这部主旋律也不输商业片，实属不易。<br />
<br />
<br />
贱国大孽演员到穗宣传。<br />
被观众誉为“数星星”的贱国大孽上映首个半日，票房达1500万，超越了《长江七号》首个半日1200万的记录（因为两部电影都是在下午起片）。据中影营销分公司蒋德富估计，该片首个周末即将过亿，而且总票房有望突破三亿。<br />
