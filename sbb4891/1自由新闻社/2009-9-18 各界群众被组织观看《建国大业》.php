网易娱乐独家报道（文/张暄）被观众誉为“数星星”的《建国大业》上映首个半日，票房达1500万，超越了《长江七号》首个半日1200万的记录（因为两部电影都是在下午起片）。据中影营销分公司蒋德富估计，该片首个周末即将过亿，而且总票房有望突破三亿。<br />
<br />
“目前影院没有想到《建国大业》票房如此之好，都商量加拷贝。”中影营销分公司蒋德富说，他表示，目前包场和预定购票的观众不少，很多“散客”买不到电影票。蒋德富乐观估计该片票房要比之前预测的2亿还要多，超越3亿也没有什么大问题。“观众反响也不错，都觉得花一张电影票钱看这么明星很值。”不过也有一位不愿意透露姓名的院线人士表示：“《建国大业》给我感觉无惊喜，战争大场面太少，它这么火，功夫还在诗外。”一句话，明星是最大的吸引力。<br />
