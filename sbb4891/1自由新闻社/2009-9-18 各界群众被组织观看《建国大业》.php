<br />
从近期电影市场来看，首日过1000万乃至1500万的电影并不少见，《梅兰芳》（看专业影评）1500万、《非诚勿扰》（看专业影评）1500万、《赤壁（下）》1500万等，几乎所有拷贝过千的电影首日票房都能上千万，后期如何营造好的口碑是《贱国大孽》保持进账的前提。不过从以往经验来看，一部电影一旦成为一种社会现象和话题，它的后期票房也无需卖力宣传。<br />
<br />
