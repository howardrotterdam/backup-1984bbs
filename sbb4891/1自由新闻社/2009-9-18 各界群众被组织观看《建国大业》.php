													<h2>萧瀚：看《建国大业》</h2>
						
						
																											<div id="postmessage_248893" class="t_msgfont">看《建国大业》<br />
<br />
 <br />
<br />
萧瀚<br />
<br />
 <br />
<br />
 <br />
<br />
1. 有点意外，这电影居然也还能看。虽然把灾难片拍成功德片，把恐怖片拍成马屁片这老套路没改，平心而论，有些情节还是有点看头的。<br />
<br />
 <br />
<br />
2. 蒋介石的台词：“反腐败，反吧，亡党，不反吧，亡国。”这样的台词也可算是煞费苦心了。<br />
<br />
 <br />
<br />
3. 剧情主线是第一届政治协商会议，这颇耐人寻味――本意当然是要重申当时政权的合法性以论证当下的合法性，不过效果上是不是力证当时政权合法反倒没法论证当下了？<br />
<br />
 <br />
<br />
4. 不管怎样，第一届政治协商会议时候的花瓶，比现在的花瓶做工还是要精良一些。<br />
<br />
 <br />
<br />
5. 韩三平还是挺会拍的，无论影片还是马屁。<br />
<br />
 <br />
<br />
6. 这种电影当然不可能塑造出什么像样的银幕人物，不过，蒋介石、蒋经国都给人留下不错的印象，蒋经国甚至还很正气，这也基本符合历史事实，至少不脸谱化――虽然陈坤演蒋经国还是太帅了些。<br />
<br />
 <br />
<br />
7. 许晴演的宋庆龄，漂亮是漂亮，不过宋刚毅的一面似乎没有机会在这片子里得到展示，诠释国旗那段戏，倒是把宋庆龄的政治低能给演出来了――她确实有这么低能，想想“大跃进”时候她在自家后院筑炉炼钢就清楚了。<br />
<br />
 <br />
<br />
8. 邬君梅演的宋美龄也很漂亮――是的，人家宋美龄本来就漂亮。这种电影唯一对党国不利的就是国民党方面的人物，个个都很帅气、优雅――虽然在政治上他们未必是好东西，老共的人物个个都很土鳖――但土鳖并不能自证就是好东西。<br />
<br />
 <br />
<br />
9. 看闻一多被杀那段，觉得时代还是有些进步，像闻一多那样在学校演讲，放到现在的话，被暗中谋杀的可能性不大――虽然监狱是随时开门候着的。<br />
<br />
 <br />
<br />
10. 所谓明星云集无非是要赚票房，明星之所以能云集当然因为韩三平的号召力――不是他这人的号召力，而是他作为中影集团董事长的号召力，就像在那时候的上海，你敢不买杜老板的帐？<br />
<br />
 <br />
<br />
11.&nbsp;&nbsp;明星云集，一定程度上可以让人感受一下演艺界大腕们的骨头轻重。很遗憾，连陈道明、郭德纲都加入这部片子的跑龙套行列，不知道他们在别的片子里愿不愿意演个很次要的角色。老谋子没去丢人现眼，估计是因为觉得自己该是统帅，不能去争当十大元帅。<br />
<br />
 <br />
<br />
12.&nbsp;&nbsp;这部片子因为是男人戏，女影星们的脊梁骨相对来说比较容易得到保存，比如就没有巩俐。像林芳兵这样人品戏品都一流的人，没有在那上面露脸还是挺让我欣慰的。<br />
<br />
 <br />
<br />
13.&nbsp;&nbsp;近二百号明星出演这电影，不少影星还为轮不着他们而遗憾，这演艺界的势利嘴脸很容易让人想到一句名诗：“十四万人齐解甲，更无一个是男儿。”<br />
<br />
 <br />
<br />
14.&nbsp;&nbsp;好在还有场外孙海英的一句经典台词，给中国演艺界的男人们补台：“《建国大业》是什么？我不知道！韩三平我知道，黄建新我也知道，但是《建国大业》我真不知道！”――无论他是真不知道还是假不知道。<br />
<br />
 <br />
