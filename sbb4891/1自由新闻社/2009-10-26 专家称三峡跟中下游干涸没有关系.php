<br />
　今年9月以来，长江流域来水明显偏少，洞庭湖、鄱阳湖两湖出现严重旱情。按照国家防总、水利部的统一部署，长江防总紧急调整了三峡水库蓄水计划，要求三峡水库从10月19日16时起，按不小于8000立方米/秒控制下泄。<br />
<br />
