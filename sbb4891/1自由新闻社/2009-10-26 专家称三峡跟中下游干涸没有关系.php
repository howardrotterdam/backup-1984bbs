													<h2>不应忘记的台湾省人大代表――黄顺兴</h2>
						
						
																											<div id="postmessage_313380" class="t_msgfont">发表于 2008-11-01 22:32:46 <br />
连贴三篇都被删,凤凰真是独裁者听话的乖孙子.<br />
<br />
 在1988年七届人大一次会议上，出现了人大会议有史以来的第一张反对票，这打破了人大几十年来全票一致通过的历史。自此人大代表也开始走向理性说不，表明人大不应是像皮图章，因而被视为中国民主化的重要进程。这位投反对票的代表名叫黄顺兴，谁也想不到，人民共和国最高权力机关的第一张反对票，竟然是由一位来自宝岛台湾的同胞投出。<br />
<br />
&nbsp; &nbsp; 由于众所周知的原因，台湾隔着一湾海峡，国家没有统一。但全国人大依然有台湾省的名额，其代表一般从生活在大陆的台湾籍人士中产生。正因为这一点，台湾省人大代表能否代表台湾民众，屡屡受到质疑。但是这位投下反对票的黄顺兴，其来历还真是不一般。他曾经是台东县长，并连任两届台湾立法机关的“立法委员”。一人能分别担任两岸立法机构的“立法委员”和“全国人大代表”，这恐怕还真难找出第二个人了。我们来看看他的经历。<br />
<br />
&nbsp; &nbsp; 黄顺兴(1923--2002)，台湾彰化人。1942年日本熊本农业高等学校毕业，农业及环境保护专家。曾任中国农科院顾问，第七届全国人民代表大会常务委员会委员。<br />
<br />
&nbsp; &nbsp; 1964年出任第五届台东县长，被人称为“平民县长”，经常下水田与农民一起劳动，走访山地，为原住民请命。1972年当选增额&quot;立委&quot;，1975年竞选连任。他积极支持爱国统一运动，被台湾爱国人士视为在政界支持统一的一面大旗。1985年10月经日本前来中国大陆定居。 <br />
<br />
&nbsp; &nbsp; 1988年任中华人民共和国全国人大常委。1988年七届全国人大一次会议上,黄顺兴代表投下全国人民代表大会历史上的第一张反对票,人大代表开始依法理性说“不”。与此同时，黄顺兴认为应该设立“秘密投票处”。因为参加投票的人挨得很近，这样以来，投票的结果都能被看到，违背了秘密投票原则，也侵犯了代表的权利。黄老的这个建议立即被大会采纳。全国人大本来没有大会发言这一项，黄老提出，不管小组讨论怎么样，大会是全体代表沟通的最后一个机会，这个权利不可被剥夺。在黄的一再坚持下，有几位委员也明白表态赞同，1989年通过了人大《议事规则》。<br />
<br />
&nbsp; &nbsp; <strong>他曾多次为反对三峡工程上马奔走呼号，是国内外著名的三峡问题专家。在1992年3月7届人大5次会议，黄顺兴于1992年3月31日向大会秘书处登记要求大会发言。根据《议事规则》，主席必须安排发言，但是到表决前的最后一刻，黄顺兴都没有被安排发言。他当即在座位上举起手，要求即席发言，但主席万里不予理睬。他从自己的位子上站起来坚决要求发言，此时，全场扩音系统突然关闭了，就只剩下大会主席面前的一个麦克风还在工作。黄顺兴等25名代表以退场、不按表决器表示抗议。他仍说：“大水又不会淹到我台湾去．我是站在全国人民的立场上的。”</strong> <br />
<br />
&nbsp; &nbsp;1992年三峡案在完全没有公开辩论的情况下强行通过，赞成票1767票，占出席人数（2633人）的67%；反对177票；弃权664票；而未按表决器的25票，正是黄等人离席抗议的结果。1/3的人大委员投了反对票或者弃权票，这是中共政治史上第一次投票危机。面对记者，黄顺兴当场表明不愿再当这个人大代表。<br />
<br />
&nbsp; &nbsp; 他曾经去过内蒙考察，警告中国已面临严重的沙化问题，在他积极奔走下，间接促成了全国人大成立环境保护委员会。 <br />
<br />
其主要著作有《环球之旅》、《历史的证言》、《台湾农业的前途》、《看大陆(1990)》、《走不完的路》、《北京见闻录(1993)》等。<br />
