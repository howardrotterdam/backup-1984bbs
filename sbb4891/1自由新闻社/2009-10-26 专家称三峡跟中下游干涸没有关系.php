旱灾通常由气候变化引致。今年夏秋，包括广东在内的诸多省份少雨干旱。但长江流域广大地区今次大旱情，却与三峡有关。据报道，三峡水库水位由9月15日的近148米上升到10月24日的170米，截留之水形成前所未有的巨大人工湖（<a href="http://news.xinhuanet.com/politics/2009-10/28/content_12348717.htm" target="_blank">http://news.xinhuanet.com/politi ... ontent_12348717.htm</a> ）。而与此同时，湖南各条长江支流水位迅速下降，洞庭湖水位降至60年来同期最低值。渔民无鱼可捕，枯水影响航运，长沙等城市食水供应告急。江西境内的4条大河逼近或低于历史同期最低水位。鄱阳湖比正常年份提前40天进入枯水期。九江、安庆、芜湖、南京等地，水位降到了1968年以来历史同期最低值。南京出现干燥的“沙漠天气”，流鼻血的人增加二、三成。（参见：<a href="http://news.163.com/09/1030/07/5MRTNKFO000120GR.html" target="_blank">http://news.163.com/09/1030/07/5MRTNKFO000120GR.html</a> ）<br />
１．洞庭湖君山岛干涸的湖底捕鱼，<br />
