													<h2>黄万里毕生反对三峡工程　预言大坝终将被迫炸掉</h2>
						
						
																											<div id="postmessage_313324" class="t_msgfont">2006-08-23 13:40:58　来源: 大众网-经济导报　 网友评论 0 条 进入论坛<br />
<br />
　　核心提示：今年是黄万里先生逝世5周年。出身名门，作为黄炎培之子的黄万里，一生中的转折点就是黄河三门峡工程上他的坚持己见。右派平反后，他曾六次上书国家领导人反对在长江三峡上建大坝。本文发表于2005年。 <br />
<br />
黄万里(1911.8.20-2001.8.27)<br />
<br />
　　半个多世纪以来， 黄万里以学识渊博、观点独到而蜚声中外，更以敢讲真话、仗义执言而在学界独树一帜。从这个意义上说， 黄万里的离世意味着，在国内重大水利工程讨论上，另一种声音的消失 <br />
<br />
　　2001年8月27日下午3时5分，在清华大学校医院一间简朴的病房，90岁的 黄万里先生溘然而逝。 <br />
<br />
　　 黄万里，清华大学水利系教授，著名水利工程专家。自1937年留学归国起，倾毕生心力于国内大江大河治理。 <br />
<br />
　　9月4日的追悼会上，他的一位学生告诉记者：在国内水利学界，多年来， 黄万里代表着科学家的良心。 <br />
<br />
　　他当年的助教回忆说，黄先生最大的特点就是为人耿直，敢说敢言，不管什么时候，不管针对谁，他都是照说不误，有时可以说是口无遮拦。在他对三门峡工程的意见中，这种性格得到了体现。 <br />
<br />
　　1957年6月，由周恩来总理主持，水利部召集70名学者和工程师在北京饭店开会，给前苏联专家的方案提意见，谈看法。参加这次会议的所有专家学者，除了一位名叫温善章的人提出改修低坝外，只有 黄万里一人，从根本上全面否定了前苏联专家的规划，其余的人异口同声，赞成三门峡大坝上马，认为三门峡大坝建成后，黄河就要清水长流了。研讨会开了10天， 黄万里参加了7天，也辩论了7天，到最后，会议就成了以他为对象的批判会。　　 <br />
<br />
　　 孤身反对三门峡工程，被划为“右派” <br />
<br />
　　1957年上半年，三门峡工程即将开工。黄万里在水文课堂上给同学们讲述了他对三门峡工程的看法，一是水库建成后很快将被泥沙淤积，结果是将下游可能的水灾移到上游成为人为的必然的灾害。二是所谓“ 圣人出黄河清 ”的说法毫无根据。因为黄河下游河床的造床质为沙土，即使从水库放出的是清水，也要将河床中的沙土挟裹而下。在课堂上，他对“圣人出黄河清”的说法甚为不屑，使人觉得这种说法实出于政治阿谀而缺乏起码的科学精神。 <br />
<br />
　　早在1956年5月，黄万里就向黄河流域规划委员会提出了《对于黄河三门峡水库现行规划方法的意见》。这篇文章刊于《中国水利》1957年第八期，并收入了《三门峡水利枢纽讨论会资料汇编》（1958年4月水利电力部印）。意见书全面否定苏联专家关于三门峡水库的规划，而不是只在个别问题上持不同意见。 在反右期间，向一个党支部书记提意见就是“反党” 。指名道姓地说邓子恢副总理的报告“不正确”。光这一句话就够右派了。 <br />
<br />
　　1958年11月25日，三门峡工程开始黄河截流。1960年6月高坝筑至340米，开始拦洪，同年9月关闸蓄水拦沙，是年潼关以上渭河大淤，淹毁良田80万亩，一个小城被迫撤离。库内的水位在涨，库区的农民一批批挥泪踏上离乡背井之路。这一切不几乎都在黄万里的预见中吗？其实，黄万里只是本着科学家的良知，说出了关于黄河及泥沙与三门峡大坝问题的科学的真话，可是他被非民主决策击败了，他成了“反党反社会主义的右派分子”。 <br />
<br />
　　于是三门峡工程改建的“两洞四管”方案确立。改建的四条钢管于1966年7月投入运用，增建的两个隧洞分别于1967年8月、1968年8月建成，水库淤积开始减缓，排沙能力依然不足，潼关以上河床“翘尾巴”淤积还在继续。 <br />
<br />
　　事实虽证明真理在黄万里一边，但令人伤心的是，有些人反而迁怒于提出正确意见的黄万里。1961年，黄万里“奉命在密云劳动，与昌黎民工同居同食同劳，所居半自地下掘土筑成。”“文革”中更贬他到三门峡挖厕所以示惩罚。 <br />
<br />
　　三门峡工程的一切问题和灾难都按黄万里的预言来了。我们从这一重大失败中总结教训，可以使我们获得大量思想资源。但传统文化的惯性却导致一些人要隐瞒真相，歪曲事实，混淆是非，为自己、为大人物、为尊者，文过饰非。　 <br />
<br />
　　 六次上书中央谈三峡问题 <br />
<br />
　　1980年2月26日，在度过了22年半的右派生涯后，黄万里终于获得了右派“改正的决定”。决定只有短短几行，称：“黄万里同志原划右派问题属于错划。经中共北京市委批准予以改正。恢复政治名誉，恢复高教二级教授的工资待遇。” <br />
<br />
　　经历了20多年的冤屈镇压，黄万里仍没学会看政治风向表达学术观点。从上三峡大坝的消息传出后，黄万里就先后给众多国家领导人上书，不遗余力地反对在长江三峡上建大坝。 <br />
<br />
　　他认为：从自然地理观点，长江大坝拦截水沙流，阻碍江口苏北每年十万亩的造陆运动；淤塞重庆以上河槽，阻断航道，壅塞将漫延到泸州、合川以上，势必毁坏四川坝田。目前测量底水输移率尚缺乏可靠的手段，河工模型动床试验在长期内长段落中尚欠合理基础，只可定性，不能定量，不足以推算长江长期堆积量。故此而论， 长江三峡大坝永不可修 。如果是为了发电，可在云贵湘鄂赣各省非航道上建大中型电站，它们的单价低、工期短，经济效益比三峡大坝发电要大四倍以上。就流域经济规划而言，也应先修四川盆地边缘山区之坝，如乌江电站等为宜。 <br />
<br />
　　此外，从国防的角度看，大坝建起来后无法确保不被敌袭，也很不安全。黄万里预言：“ 三峡高坝若修建，终将被迫炸掉。 ”同时，他还指出，公布的论证报告错误百出，必须悬崖勒马、重新审查，建议立即停止一切筹备工作，分专题公开讨论，不难得出正确的结论。 <br />
<br />
　　1986年，中共中央、国务院决定对三峡工程进行论证，黄万里教授没有被邀请参加工程论证。黄万里数次给中央领导人和政治局，国务院总理、副总理、国家监察部写信，痛述三峡工程的危害。要求中央决策层给他半个小时的时间，陈述为什么三峡工程永不可建的原因。但没有答复。 <br />
<br />
　　黄万里晚年病重昏迷中仍喃喃呼出：“三峡！三峡，三峡千万不能上！”2001年8月27日，他带着无尽的遗憾离开了人世。　　 <br />
<br />
　　 黄万里报憾辞世 <br />
<br />
　　“圣人出，黄河清”是封建时代的人文思维，带有谶纬迷信色彩，在严肃的科学论证面前，本来上不得台面。但当时的某些参与决策者，竟然将这种腐朽观念奉若神明。苏联专家不会懂得这个掌故，肯定是中国人才会搬出这种错误的人文思维作为武器，为错误的技术思维开路。该工程当时没有问责制，如今要想问责，主要决策者已经不在了，剩下几个技术官僚和科学“泰斗”，在责任上“顾左右而言他”，肉烂嘴不烂。 <br />
<br />
　　<strong>在既有三门峡和阿斯旺的前车之鉴、国内也存在众多反对意见的背景下，三峡工程议案于1992年被七届人大五次会议以1767票通过，反对177票，弃权664票，赞成票数之少，在人大历史上是空前的。</strong> <br />
<br />
　　国家机器需要“标准件”，“独立思想者”如黄万里教授者，在历次政治运动中纷纷落马，或被弃置不用，形成了人才选拔上的“精英淘汰制”，这就为好用听话的“标准件”入选创造了条件。经过“文革”对文化、道德的摧残，急功近利的技术思维逐渐占据上风，也就不足为怪了。 <br />
<br />
　　三门峡工程不足四年就现世现报，水利工程逐渐变成了“水害工程”。在难以逆转的生态灾害形成之后，如何恢复生态，能否拆除这个废物，就成了谁也负责不了的“老大难”。谁又能够想象，将来三峡工程正式退休以后，后代子孙该如何为它老人家送终？ <br />
<br />
　　黄万里在有生之年，看到自己对三门峡的意见不幸言中，痛心疾首，反复叨念：“他们没有听我一句话！”晚年病重昏迷中喃喃呼出：“三峡！三峡，三峡千万不能上！”带着无尽的遗憾离开了人世。如今三峡工程竣工，库区清污成本和长江航运成本剧增，已是不争之事实。据三峡工程防汛办提供的气象资料显示，“今年（编者按：指2005年，下同）4月份三峡坝区天气复杂和剧烈变化程度为近50年同期所少见”，请看《中国三峡工程报》的报道：“今年4月三峡坝区气候反常。气温并没随夏季的到来逐渐上升，反而呈下降趋势。4月末平均气温不足12摄氏度，4月中旬周边山区还出了较大范围的降雪，月内有3次降温过程，温差升降剧烈、颠倒错位的现象严重。另外，4月份全月降水量为236.5毫米，破坝区近10年降水量最高纪录，破宜昌地区近118年同期降水量最高纪录。” <br />
<br />
　　三峡库区地质环境复杂，暴雨、洪水频发，自古以来就多滑坡。三峡大坝坝址附近区域为坚硬的花岗岩，向上游则多以碎屑岩、碳酸岩为主,包括侏罗纪遗址的粉砂岩。地质容量、环境容量的天然不足，仅国土资源部查明的滑坡就有2490处。近两年我国南北气候反常，今年重庆地区大雨滂沱，多处发生山体滑坡。这些现象是否与生态上的变异有关，虽有待专家们继续观察论证，恐怕也无须久待。　<br />
<br />
