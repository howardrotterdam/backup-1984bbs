<br />
“主因是当地持续高温、降雨量很小导致”；三峡方面称，蓄水可解来年旱情<br />
<br />
　据新华社电 针对近期有种把三峡工程175米试验性蓄水与长江中下游干旱相联系的看法，有关部门和专家表示，三峡蓄水不是导致干旱的主要原因，而且将有助于改善长江最枯季节中下游的用水条件。<br />
<br />
　中国工程院院士、长江水利委员会总工程师郑守仁在接受记者采访时说，近期湖南、江西等省的湖泊、河流水位低，最主要的原因是这些地区自身的旱情，即9月以来当地持续晴热高温，降雨量很小，无法形成径流。<br />
<br />
　记者从中国气象局获悉，此次南方出现干旱有两大原因。一是入秋以来我国南方大部被副热带高压控制，来自北方的冷空气和来自南海和孟加拉湾的暖湿气流难以在江南和华南北部相遇，造成南方持续高温少雨。二是入秋以来登陆我国的3个热带气旋只影响到华南南部和沿海等地，未给江南和华南北部带来丰沛的降水。<br />
<br />
　中国长江三峡集团公司三峡枢纽梯级调度中心主任袁杰说，三峡蓄水对长江中下游虽有影响，但今年蓄水和用水之间的矛盾与去年相比，已小得多。<br />
<br />
　郑守仁说，三峡水库蓄至高水位，才能在来年1至2月长江上游来水量最小时给下游补水，否则届时下游的旱情会更严重。长江上游在1至2月期间的来水量最小值仅为2700立方米/秒，正常年份在3000至4000立方米/秒之间。三峡蓄水后，届时下泄流量可达5000至6000立方米/秒。<br />
<br />
