10月24日以前，诸多媒体在报道华中旱情时，都引述当地水文专家的观点，指三峡蓄水是大旱的肇因之一。10月19日14时，长江防汛抗旱总指挥部发布“ 防总办11号调度令”，紧急要求三峡总公司从19日16时起，按出库流量不小于每秒8000立方米控制下泄。24日，再次加大下泄流量，并宣布175米蓄水计划推迟到11月上旬完成。这个仓促的补救决策，说明三峡蓄水的恶果引起了当局注意。<br />
<br />
然而，10月25日，官方新华网突然发布新闻稿《三峡蓄水并非近期长江中下游干旱主因》，称“近期有媒体把三峡工程175米试验性蓄水与长江中下游干旱相联系，有关部门和专家表示，三峡蓄水不仅不是导致干旱的主要原因，而且将有助于改善长江最枯季节中下游的用水条件。” 显然，有关部门开始封杀对三峡工程的负面议论。<br />
<br />
但三峡加大泄水流量后，旱区水系水位止落回涨的速度缓慢，华中旱象持续恶化。28日，三峡水库第三次加大下泄流量。当天中午，中央电视台突然报道，为缓解中下游旱情，有关部门决定调整三峡蓄水计划，上游来多少水就放多少水，并透露在年内已不可能实现175米高程蓄水（<a href="http://news.xinhuanet.com/video/2009-10/28/content_12346738.htm" target="_blank">http://news.xinhuanet.com/video/2009-10/28/content_12346738.htm</a> ）。然而蹊跷的是，这条重要消息鲜有媒体转载。<br />
<br />
事情已经非常清楚，三峡蓄水加剧了华中大旱。这是关乎国计民生的重大公共事件，造成重大失误的责任部门难辞其咎，然而有关部门却讳莫如深。他们躲躲闪闪，迟迟不向公众公开透明地交代真相。长江水利委员会的专家把华中干旱的原因归咎于天气，称三峡蓄水是为了抗旱，蓄至高水位，才能在来年苦水季给下游补水。直到采取加大下泄流量的紧急措施后，有媒体还称赞这是三峡工程“对长江中下游的补水功能初步显现”。<br />
<br />
这是对公众极不负责的态度，好比一个人先劫掠，再施舍，然后以扶困救难的善士自居。请不要再欺骗老百姓，请解释：明知夏秋之际中国中部、南部少雨干旱，为什么还启动风险如此巨大的三峡蓄水计划？这次华中大旱，将造成多严重的经济损失和环境破坏？ 175米蓄水的潜在问题，三峡工程对环境（包括气候）的宏观影响，国务院和全国人大有没有严格的监察？<br />
<br />
国人皆知，三峡工程是政治工程。上世纪80年代，在三峡工程论证中，曾有强大的反对声浪。日后的政治高压下，工程建设计划在人大获得通过。此后，反对意见无从表达，试图监督三峡工程、揭露工程弊端的传媒一再被打压。媒体上充斥着对三峡工程的溢美之辞，赞扬工程初期运行开始发挥防洪、发电、航运等综合效益，原来论证的总结论和建设方案完全正确。<br />
<br />
为什么在今秋急急上马175米高程蓄水？有关部门宣称“将兼顾上、下游需求，正确处理防洪、发电、航运和供水之间的关系”，但一个月来长江中下游航运淤滞、供水告急的事实，证明这完全是一句假话。<br />
<br />
能源部门这个央企巨无霸，真正追求的东西只有一个――钱。<br />
<br />
&nbsp; &nbsp; 三峡总公司副总经理曹广晶，在蓄水开始时对记者道出真言：<br />
<br />
&nbsp; &nbsp; 只有蓄水到175米水位，安装的26台70万千瓦巨型机组才能在高水头下达到其设计能力。机组全部投产后，年均发电量将达847亿千瓦时，比水位在156米时增加100多亿千瓦时，相当于10个大亚湾核电站。<br />
<br />
另据报道，今年5月，以“长江电力”为平台，三峡总公司主营业务的整体上市，目标资产规模高达上千亿元，长江电力将成为全球最大的水电上市公司。<br />
<br />
水――电――钱，<br />
<br />
秘密在这里：中国正处在“国进民退”的时期，国家资本疯狂控制资源，特殊集团凶猛聚敛财富。<br />
<br />
然而今次三峡蓄水，也触犯了众多诸侯，导致各省怒气冲天。三峡175米蓄水计划高调启动又戛然而止，正反映出内地紊乱的现状。祸已酿成，责必追究，体制的裂隙正在扩大。</div>

							
							
							
							
