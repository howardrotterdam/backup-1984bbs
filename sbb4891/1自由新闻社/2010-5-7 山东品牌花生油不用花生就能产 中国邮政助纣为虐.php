													<h2>山东品牌花生油不用花生就能产 中国邮政助纣为虐</h2>
						
						
																											<div id="postmessage_603476" class="t_msgfont">山东品牌花生油不用花生就能产 李金斗代言<br />
2010年05月06日 22:15中国质量万里行<br />
胜福食品公司大门（资料图）<br />
胜福食品公司的生产车间（非正常拍摄）<br />
<br />
花生油营养丰富、口感好，是消费者食用较多的生活用油。而山东一家年产量千万斤的油脂厂，竟然用棉籽油和大豆油加入花生油香精进行勾兑，并且“贴上什么牌子就是什么牌子，油都是一样的油”。随后精美的外包装和名人广告效应，创造出了价格不菲的“压榨一级花生油”。中国质量万里行记者通过15天时间、行程2000多公里的明察暗访，了解到这些伪冒花生油借助邮政物流、大型超市等销售渠道，销往山东、河北、山西、江苏数省城乡，年销售额数千万元。 <br />
<br />
文/张伟龙 本刊记者 刘畅<br />
<br />
胡光宇在山东经营粮油生意10余年，一直关注食用油市场的变化，他的总体感受是：食用油品牌层出不穷，市场竞争日益惨烈，油的质量也一年比一年令人担忧。<br />
<br />
胡光宇说，我国目前食用油市场的容量和前景令人眩目，上个世纪80年代，我国食用油年消费量不到600万吨，现在销量达几千万吨。特别是老百姓生活品质提高了都喜欢吃花生油。但是有一个奇怪的现象：花生原料的增长幅度是有限的，但市场对花生油的需求及花生油的销量一直在增长，油原料和油成品增长之间的差距非常大。原料不足油产量怎么会一直在增长呢？<br />
<br />
不用花生就能产出花生油 <br />
<br />
胡光宇曾在淄博做生意，主要经营粮油，近日他向中国质量万里行披露了棉籽油、大豆油、花生油香精勾兑，冒充花生油的内幕。<br />
<br />
“任何一种食用油勾兑花生油香精就等于压榨一级花生油。”<br />
<br />
胡光宇说。他以前经营的所谓纯正花生油大都是用精炼棉籽油和大豆色拉油勾兑的。<br />
<br />
一般勾兑花生油大都是用大豆色拉油和精炼棉籽油，这样勾兑出来的花生油很香，而一般人买花生油只看颜色、闻香味，特别是添加花生油香精之后，勾兑的花生油从外观、香味上根本辨别不出真假。<br />
<br />
胡光宇现在虽然有房有车，在别人看来生意做得很成功，但他却自认为在做人方面很失败。因为他所谓的成功是靠销售“勾兑油”得来的。<br />
<br />
胡光宇想着自己多年来经营的“坑人的生意”，愧疚感无法用言语表达。<br />
<br />
胡光宇表情痛苦地说，他虽然不再销售“勾兑油”了，但这种油在市场上依旧很多。按照胡光宇提供的线索，记者经过半个多月的调查，揭开了“勾兑油”的重重黑幕。<br />
<br />
记者乔装代理商与厂家接头<br />
<br />
“你尽管上网查吧，几乎任何一家招商网上都有花生油的信息。”胡光宇说。<br />
<br />
3月6日，记者在当地的一家招商网上，看到了著名笑星李金斗代言的花生油产品，有鲁大、六福、沂蒙花、金秋庄园等系列。<br />
<br />
公司网站上显示的是，淄博胜福有限公司，地址栏显示：淄博市张店经济开发区，联系人，李艳峰。<br />
<br />
该公司网站显示，这是一家以生产销售花生油、大豆油、玉米油、调和油、冷榨原生花生油为主的多种食品的综合高新技术企业。公司还声称在沂蒙山区拥有固定的万亩优质无公害花生原料基地和油脂生产压榨基地。<br />
<br />
按照胜福公司标注的联系方式，记者以食用油代理商的身份联系上了李艳峰。听到代理胜福公司的产品，李颇感兴趣。<br />
<br />
双方约定当日下午在淄博市人民东路一加油站处见面。<br />
<br />
随后，记者见到了开着一辆小型厢货卡车的李艳峰。<br />
<br />
经介绍，李艳峰主要负责山东市场的销售，同时也分管外省的代理商。<br />
<br />
“现在正上货，主要是超市里，在市区就用这种小型的卡车。”李经理说。记者看到，车厢里还有几箱花生油，箱子上印着“鲁大”的商标，标示的是压榨一级花生油。<br />
<br />
记者表示，要在一个省份代理胜福的产品。李艳峰主动邀请记者到厂里参观。 <br />
<br />
工厂建在开发区的隐蔽胡同里<br />
<br />
3月13日，中国质量万里行记者随李艳峰来到淄博胜福食品有限公司(以下简称胜福)。<br />
<br />
胜福的工厂在淄博市张店经济开发区一个比较隐蔽的胡同里，工厂的大门没有任何标识，从外面看不出来是生产食用油的厂家。<br />
<br />
一进工厂大门，记者看到院子里摆放着一堆沾满了油渍的白色塑料桶，表面凹凸不平，显然已反复使用过多次。有的塑料桶没有盖子，里面落满了灰尘，散落在生产车间旁边，在车间门外几个没有穿任何工作服的工人正在把小包装油桶里的油倒进一个大的油桶里。<br />
<br />
李艳峰指了指院子里的几个大罐子告诉记者，这些罐子都是用来装油的。记者看到，罐子的阀门上粘满油脂和灰尘，大罐子旁边的塑料桶已脏得看不出原貌。<br />
<br />
他拿出了胜福公司主要推广的几个品牌，鲁大、沂蒙花、六福、金秋庄园。“鲁大和沂蒙花卖得最好。”李艳峰说，“贴上什么牌子就是什么牌子，油都是一样的油”。<br />
<br />
为了吸引记者代理胜福的花生油，李艳峰随即表明了公司的优惠政策――为代理客户无条件退货，最大限度地减小代理客户的风险，没有库存压力。<br />
<br />
中国质量万里行记者表示，只能在网站看到，胜福公司于2002年成立注册，固定资产1000万元，还要看一下生产线，才能真正了解胜福的实力。<br />
<br />
随后，李艳峰带领记者进入了工厂的包装间，并声称生产车间属于技术保密的范围，不能随便参观，只能看包装间和仓库。“现在是花生油的淡季，工人基本都放假了。”李艳峰说。记者看到，在包装车间里，有两三个工人在贴标签，没有穿工作服，满手的油污。<br />
<br />
李艳峰介绍，一共有两条灌装线，其中一条每小时能灌装160多桶油，另一条也能装100桶左右，因此不管客户有多少需求量，公司都能够满足。<br />
<br />
紧挨着灌装线靠墙的位置，是白色塑料桶装的黄色油，李艳峰称这些是“散油”。有的塑料桶底部有黑色的沉淀物，还有一些塑料桶中的油出现几层的颜色。正在工作的工人称，这些是加工花生油的原料油。<br />
<br />
灌装线的出口处，散落着一地的标签，有些标签纸已经被踩得变形，还可以看出鲁大、沂蒙花、六福等字样。<br />
<br />
库房和包装间其实在一间大厂房里，中间只有一条通道，是工人来回拉货用的。各种牌子的“花生油”堆积了有两人多高。<br />
<br />
正在搬着一箱油的一个工人说，堆积的这一点货，到卖油旺季的时候，一天就没有了<br />
年销5000多吨 一部分销往大型超市<br />
<br />
参观了包装间和仓库之后，李艳峰把一桶5升装的“鲁大”拿到桌子上，“像这种的，代理价44元一桶”。<br />
<br />
“油质量怎么样？”记者边问边装得比较老练的样子，把油桶盖子打开，用手指沾了一点油，放在鼻子前闻了闻，又放在嘴里细细的品尝了一下，说：“这油做得还不错，包装也比较大气”。<br />
<br />
李艳峰一看记者是“内行”，赶紧道：“说句实话，这是用豆色拉油和棉籽油勾兑的，不过质量没问题”。花红柳绿的宣传页上打着“质量第一，信誉至上”的企业宗旨。<br />
<br />
他说，如果记者代理胜福公司5升装的压榨一级花生油，每桶44元，而鲁大、沂蒙花、六福、金秋庄园几个品牌，如果卖到公司规定的数量，还有1%的返点。<br />
<br />
李艳峰表示，2009年的效益不是很好，也就销售了5000多吨。主要是在山东市场，而山西、河北、江苏也都有胜福公司的产品。<br />
<br />
“主要走的是团购和福利，”李艳峰说。他举例，河北的市场做得比较好，一年有300多万元的销售额，山西也有，做得也差不多，比较成熟的还属山东市场，仅在邹城――还没有淄博一个张店区地方大，一个中秋节就能卖到100多万元的销售额。<br />
<br />
记者临走时又向李艳峰要了数张印着李金斗照片的宣传页。据李艳峰告诉记者，李金斗是胜福公司形象代言人，每年的代言费7万元。<br />
<br />
做纯花生油是“赔本”买卖<br />
<br />
看到中国质量万里行记者提着的“压榨一级花生油”，常年榨油的农户刘老汉笑了。他拍着胸脯说：“你买的肯定是假油，10斤(5升左右)装的花生油要在80块钱以上，你才能吃上真正的花生油。”<br />
<br />
胡光宇告诉记者，1斤花生只能榨出4两油。花生的价钱每斤3.8元左右，2斤花生还不能榨出1斤油，榨出来1斤油的成本价也就是7.6元，减去油饼渣每斤1元的价钱，每斤油的成本也就是7.5元左右，5升装的压榨一级花生油每桶卖到75元钱还保不住本钱。<br />
<br />
“他们要是卖71元钱，每桶赔4块钱，这是赔本的买卖。”胡光宇说。<br />
<br />
胡光宇进一步指出，这是单算花生的榨油关系，其他的都没有算进去。生产需要设备，运输需要车辆，车辆要烧油，加上人工、工商、税务。所有的费用都算进去，每桶油都要赔10块钱以上。<br />
<br />
3月25日，中国粮油信息网显示，全国的花生价格最低是每斤3.4元，最高每斤4.1元。山东省富产花生的泰安、临沂、潍坊等地区的花生价格徘徊在3.8元左右。<br />
<br />
淄博地区的花生油报价，一级浓香花生油厂家报价在14600元/吨，一级压榨花生油批发报价在17000元/吨。按照批发价来计算，淄博市的花生油每斤是8.5元，也就是说，胜福公司每出手一桶压榨一级花生油，就要赔14元钱。<br />
<br />
“做生意的都不是傻子，肯定要在油里做文章，只能勾兑。”胡光宇坦言。<br />
<br />
他介绍，精炼棉籽油、大豆色拉油加香精是当前比较先进的勾兑工艺，无论从味道、色泽、透明度上，勾兑出来的“花生油”都是上乘的。<br />
<br />
精炼棉籽油的市场价格每吨是7800元左右，大豆色拉油的市场价格每吨是7500元左右。二者按照7：3的比例进行勾兑，每吨油加上一斤左右的花生油香精，就成为“压榨一级花生油”。<br />
<br />
每斤香精的价格是100到200元，这样算下来，每斤“压榨一级花生油”的花生油的价格大概是40元左右。<br />
<br />
勾兑花生油的香精，主要来自广东地区。在深圳从事香料买卖的张老板称，最近几年他的生意一直很好，开春后的几个月，香精是供不应求。<br />
<br />
胡光宇透露，在所有的勾兑油当中，棉籽油和大豆色拉油算是质量较好的，有时候还有厂家使用“地沟油”勾兑，成本价1斤最多两块多钱，照此推算，市场上一些“压榨一级花生油”更便宜。<br />
<br />
在暗访中，记者发现，勾兑油已经成为行业里的一个潜规则。这是因为做真正的花生油生意，利润很低。胡光宇说，在他刚开始做花生油生意的时候，也做真的。可是别人做假的，跟他卖同样的价钱，甚至比他的价钱卖的还高，愣是比他的销路好。 <br />
<br />
“主打邮政物流，要给领导提成”<br />
<br />
“市场上销售的只占小部分，主打方向是走邮政物流，还有一部分是走福利的。”胡光宇道出“勾兑油”的销售渠道。<br />
<br />
他的说法在胜福公司负责销售的经理李艳峰这里也得到了验证。<br />
<br />
3月15日，中国质量万里行记者再次约李艳峰“洽谈”代理其公司花生油的事宜，李表示胜福公司愿意签订代理合同。<br />
<br />
双方提及邮政物流如何进行操作时，李艳峰兴奋不已。他声称，主要是要和领导搞好关系。“都会给领导提成，有的是放在明面上说，有的是在事后”。<br />
<br />
胡光宇称，在邮政物流，每年上级都会往下一级指派一定的任务，所以只要疏通好关系，就没有卖不出去的花生油。<br />
<br />
李艳峰透露，邮政物流之所以没有销售知名品牌花生油的原因是，知名品牌的花生油价格太高了，中间没有什么利润可图。在山东省，邮政物流做得最好的地区是潍坊，每年都有“千把吨”。<br />
<br />
“千把吨”从李艳峰的嘴里说出来很随意。他预言，山东省的邮政物流都会有，2010年的花生油销售高峰期来得会比较早，已经做好打市场的准备。<br />
<br />
3月23日，记者以某服装厂采购职工福利为名来到山东潍坊中邮昌邑物流中心。<br />
<br />
记者看到，在物流配送中心，“鲁大”牌花生油有5升装和1.8升装两种，5升装的零售价格是71元，1.8升装的零售价格是30元一桶，两桶为一箱。包装瓶上标有“压榨一级花生油”字样，还印着“鲁大花生油，香得有理由”的广告语。<br />
<br />
工作人员对记者保证：“油的质量你们放心就可以了，我们跟淄博的鲁大厂家已经合作五六年了，就连我们自己中邮的职工过年过节也是发这种油。”<br />
<br />
记者以让单位领导看样品为由，购买了5升装鲁大花生油一桶，71元。随后，中邮物流中心一位姓林的工作人员开具了收款收据。<br />
<br />
记者在走访中发现，邮政物流配送中心经营的粮油类商品，主要输送到乡镇以及比较偏僻的农村地区。几乎所有的邮政网点，首先映入人们眼帘的是一块绿底黄字、写有“中国邮政，质量放心”的大牌子。<br />
<br />
资料显示，截止2009年，山东省2068个乡镇建有邮政物流配送中心；并通过加盟、联合等方式，在行政村设立8万多个邮政“三农”服务站，行政村覆盖率达到95%以上，基本实现“一村一站”。 <br />
<br />
“这么多的邮政网点，配送的产品不仅涉及化肥、农药、种子、饲料等农资产品，还有酒水、洗化、食用油等日用消费品的分销，”该知情人感慨，“日用消费品的配送额在去年有90多个亿。”<br />
<br />
据该知情人介绍，近几年，山东省工商局和邮政曾经联合下达指示，各级工商登记机关为山东中邮发展设立“绿色窗口”，提供预约、集中办证等服务。<br />
<br />
在2009年9月份的山东红盾网上，有一篇名为《发挥工商职能 促进邮政企业进农村发展》的文件。<br />
<br />
该文件显示，山东省工商局将与邮政局联合出台综合配套政策，明确邮政“三农服务站”市场主体登记的前置审批、经营范围、监管等相关问题，支持邮政“三农”服务网点规范发展。<br />
<br />
“邮政物流无假货，产品质量信得过”一直是邮政物流努力打造的品牌。然而，在邮政涉足农资和粮油产品的初期，工商部门也对其进行了严密的监管。<br />
<br />
其中的措施中就有，对不符合连锁经营条件的坚决不予登记；对发现有违章违法行为的连锁店，除依法查处外，责令其退出连锁经营。<br />
<br />
“后来省工商局与邮政联合下文，工商对邮政的监管力度越来越小，直到最后放开市场监管，邮政物流的就是监管的真空地带了。”该知情人自曝。<br />
<br />
在老百姓的心目当中，邮政是国有大型企业，又是几十年的老牌子，销路畅通是自然而然的。 <br />
<br />
邮政物流是监管的真空地带<br />
<br />
邮政物流配送粮油产品，工商部门是如何进行监管的？<br />
<br />
3月20日，中国质量万里行记者采访了工商部门一位不愿透露姓名的知情者。<br />
<br />
“为什么邮政的销路好？因为它的诚信度高，涉及的区域网络又广泛，几乎遍布每一个乡镇和农村，邮政物流涉及的商品一般是上级指派给下一级的指标和任务，在工商的监管上属于真空地带。”该知情者说。<br />
<br />
从2000年开始，中国邮政正式启动物流配送工程。<br />
<br />
2003年，中邮物流有限责任公司和山东省邮政局投资设立山东中邮物流有限责任公司，注册资本1000万元，经营范围包括食品、日用品等的销售。<br />
<br />
同年，山东省邮政积极参与农资配送服务，依托遍布农村的邮政物流网络，将所有邮政支局自办网点改造成农资配送中心。同时，在村级全部通过改造代办网点、吸收连锁加盟等形式，建立邮政“三农”服务站。<br />
<br />
2006年，为支持邮政服务“三农”工作，山东省工商局与邮政局、农业厅联合下发了《关于进一步加强邮政服务“三农”工作的意见》，明确要求工商部门支持邮政部门开办农资、生活日用品等配送服务，依法扩大经营许可范围。<br />
<br />
2007年全国政协十届五次会议上，全国政协委员王祥林提出议案，建议“开发利用邮政网点资源和信息功能，在邮政农资配送业务基础上叠加农产品[16.24 1.06%]收购、配送、分销服务”。在庞大的物流体系中，粮油产品已经成为一支主力军，粮油酒等代购、代销、代理运输、批发、零售也日益成为中邮的主要业务。<br />
<br />
识别伪冒油技术落后<br />
<br />
油脂专家说“油兑油，鬼见愁”<br />
<br />
3月22日，中国质量万里行记者前往河南工业大学粮油食品学院，寻找鉴别勾兑油的科学依据。刘玉兰是油脂工程系的教授，从事油脂研究工作多年。<br />
<br />
“油兑油，鬼见愁。”刘教授语出惊人。刘玉兰进一步解释说：“勾兑的油很难分辨出是什么油，目前的技术也只能识别出是否属于纯正的花生油，即便是用其他的油勾兑，也分辨不出来勾兑的是什么油。”<br />
<br />
刘教授说，如果是勾兑油标注“压榨一级花生油”，肯定是不符合国家相关规定的。“勾兑的油必须得标明是调和油”。<br />
<br />
刘教授说，目前我国还没有关于食用调和油的统一标准。所以每个企业调和油的标准也不一样，大部分都是都根据自己的企业标准进行配比生产，这也导致了一些生产企业打“擦边球”，此外调和油市场还存在以次充好、随意勾兑、冠名标志混乱等问题。<br />
<br />
举例说明，一种调和油里添加了99%的花生油叫花生调和油，加了1%的花生油也可以叫花生调和油。<br />
<br />
“所以，这种随意制定调和油比例的现状必须要改变，在油品外包装上也应该标识明确的比例，不然消费者都是不明不白消费”。<br />
<br />
如今，市民每天烹调要用的食用油包括很多品种的调和油。除了传统的谷物调和油外，还有诸如花生、葵花籽、玉米、橄榄、山茶、坚果等多种。<br />
<br />
在北京的一家超市内，记者仔细观察了部分调和油的外包装标签，发现上面详细标注了含有的成分，却没有注明具体的配比含量。这些调和油的成分少则由两种油料组成，多的达到近10种。<br />
<br />
国内某知名品牌的一款食用调和油，由大豆油、花生油、菜籽油、玉米油、葵花籽油、芝麻油、亚麻籽油、红花籽油等组成，但基础油是哪种就不得而知了。另一种品牌的橄榄葵花油，其中橄榄油成分多还是葵花油成分多，也无从辨别。<br />
<br />
一款5升装纯花生油的价格在130元，但另一款5升装花生调和油的价格却只有70元左右。这样较低的花生油比例就给调和油企业制造了巨大的利润空间。<br />
<br />
令人关注的是有的不法企业虽在产品标签上称含有花生油和芝麻油，其实却是用花生油和芝麻油香精来替代。<br />
<br />
消费者毕竟不是行业专家，多数人表示从未真正关注过纯植物油和调和油的差别，两者价格相差那么大，只能按哪种价格实惠点就买哪种。<br />
<br />
大部分的调和油以单品种油品命名的调和油居多，如花生调和油，其中的成分也有六七种之多。<br />
<br />
超市的食用油货架旁边，一位家庭主妇先后对照了一桶纯花生油和一桶花生调和油，之后便随手拎起一桶花生调和油放进了购物车。她认为，看外包装标注的花生调和油，应该是花生油的比例和分量占大部分吧。<br />
<br />
而有些商家则把单品的名称用上大大的字号，把“调和油”三个字打印得很小，不仔细看都看不清。<br />
<br />
《食用植物调和油》标准从2005年开始制定，2009年2月开始征集意见。在草案中首次提出，当以某种或某类原料成品油对产品进行冠名时，应注明该种或该类原料成品油的实际含量，且文字和数字的高度不得小于1.8mm。<br />
<br />
当以某种功能性因子(成分)对产品进行冠名时，应注明该功能性因子(物质)的学名和实际含量，且文字和数字的高度不得小于1.8mm。另外还应标注所添加的香精、香料。届时，消费者选择调和油将可通过标签来了解其具体成分含量，进而更加明明白白地消费食用油。<br />
<br />
(文中胡光宇为化名――编者注)</div>

							
							
							
							
