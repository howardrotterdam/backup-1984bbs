我以前经常吃 </blockquote></div>　　棉籽油，即从棉花种籽（棉籽）榨出来的油。经现代科学检测，棉籽油中所含的棉酚，具有抑制男子生育功能的作用。棉酚在高温强碱条件下会分解消失，但用旧法榨油，不能彻底清除棉酚。因此长期食用旧法榨制的棉籽油，会造成不育。我国现在还有一些农村的居民，因一直食用棉籽油而造成多年不育的。但是如果停止食用，过一段时间后，生育能力仍有可能恢复。因此之故，有人称棉籽油为“安全可靠”的“强力避孕药”。 <br />
　　棉籽油可食用，但迄今我们尚未发现有明代江浙有将棉籽油作为食用油的记载，故推测当时食用棉籽油尚未普遍。到了清代，棉籽油的食用在江浙已经很普遍，以至在不产棉花的湖州，棉籽油（被称为“青油”）也被地方志列为当地主要食用油之一，而且“市肆每以掺入菜油”出售。为什么要将棉籽油掺入菜油出售？主要是因为棉籽油价格较菜油低廉，商家借掺假以牟利之故。因此，褚华《木棉谱》说：“木棉子……榨为油，……色紫而浑，以之注灯则不明，以之和蔬则味酵，但其值颇贱，市肆间私买之，以为菜油、豆油之兰亭赝本。”杨光辅《淞南乐府》也说：“棉花核榨油，用以照夜，若煮食不及菜、豆油，故价值亦贱。”郑光祖《醒世一斑录・杂录》卷六“棉花价”条则说：“棉籽为油，（价）不足计。”可见确实是一种最廉价的食用油。因此，尽管清代江浙人民可能尚未明确认识到棉籽油的避孕作用，但是由于他们经常食用之，所以实际上可以说已经在不自觉地服用这种“强力避孕药”了。当然，由于棉籽油在清代江浙食用油结构中所占比重不高（更多食用的是菜油和豆油），人们一年内食用棉籽油的总量不会太高，而且如上所述，棉籽油常常是搀在菜油中食用，每次摄取数量有限，因此它抑制生育的效果，可能不像在那些长期主要食用棉籽油的地方得那么明显。不过，由于经常食用，肯定对男性生育能力会有一定的抑制。由于棉籽油味道不佳，而且价格便宜，食用者自然主要是贫苦民众，这使得这些民众在无意中也采用了一种最廉价的节育手段。这一点十分重要，因为与比较富裕和受过教育的阶层相比，贫苦民众对于节育的愿望比较微弱，而可采用的手段也比较欠缺。</div>

							
							
							
							
