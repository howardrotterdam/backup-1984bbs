													<h2>中美合研“空间能发生器” 取之不尽的清洁能源</h2>
						
						
																											<div id="postmessage_699394" class="t_msgfont">星岛环球网消息&nbsp; &nbsp; 全球气候不断变化，地球资源有限，科学家需要尝试开拓更广阔的能源领域。美国“ OMN -搭乘实验室”和中国爱尼特公司联合实验室去年合力研发一项全新发电技术- “空间能发生器” ，透过多维空间直接获取及输出更多能量，可应用在民用或军事用途上。<br />
<br />
&nbsp; &nbsp; 美国OMNI-JCT LAB和中国爱尼特公司联合实验室6日在香港红|海逸酒店举办新闻发布会，向全世界展示其共同研发制造的一项重大科技成果――空间能发生器。运用这项技术发电，不用燃料、水，获取的是无污染、无幅射、不发热的清洁冷能源。有关机电和能源专家、产业界、媒体界人士，将共同通过一台实验样机见证这一新技术的真实性及可靠性。<br />
<br />
&nbsp; &nbsp; 爱因斯坦曾经说过，人类的重大科技突破大都来自灵感而非逻辑推理，而科学的作用则是证明它。当科技突破被科学证实之后，便成了相应的理论学说。“空间能发生器”可能也是如此。联合实验室最初研发出的是“高效能风力细胞发电机组”及“电动车电池充电技术”，在此基础上，“空间能发生器”终于诞生。<br />
<br />
&nbsp; &nbsp; “高效能风力细胞发电机组”结合风力、太阳能及“细胞式磁浮发电系统”，能使传统风能、太阳能、生物能发电机的发电功率提高3-5倍。该公司正在与数家风能、太阳能、生物能发电公司洽谈合作，如在原有发电系统上安装这种高效能发电机组，将直接增加原有设备的发电功率，产生直接经济效益。<br />
<br />
&nbsp; &nbsp; 该公司拥有的“电动车电池充电技术”，运用“磁浮充磁”、“细胞式磁浮发电”及“反引力磁浮发电”等专利系统及技术，使电动汽车能连续行驶1500英哩以上。此项技术将极大地改变现有汽车业的现状。目前该公司己完成实验室实验样品汽车，可在道路上行驶。<br />
<br />
&nbsp; &nbsp; 这次展示的“空间能发生器”技术是一项更重大的科技突破。它有可能令人类从传统的能量转换时代步入空间能量获取时代。能量转换指任何能量均转换自其它能源，因其遵循现在已有物理学的能量守衡及万有引力定律，所以其能量在转换中会逐步衰减，如热能、动能、光能、核能等都如此。而空间能获取不是靠能量转换，而是从多维空间中直接获取， 遵循的是多维空间物理学，因为是直接从多维空间获取能源，所以其获取的能量不仅不衰减，而且可以任意获取。<br />
<br />
&nbsp; &nbsp; “空间能发生器”在去年9月诞生，据称能够将能量透过一个小空间，转化并输出多10多倍的能量。负责这项研究的“ OMN -搭乘实验室”主席陈宇廷昨日指出，空间能的获取，不是靠能量转换，而是遵循多维空间物理学，从多维空间中直接获取，所以能量不仅不衰退，而且可以任意获取。省成本环保用途广泛。<br />
<br />
&nbsp; &nbsp; 由于本技术为仅需一次性投入的永续发电电源，发电成本近乎零，可替代所有己知的发电方法、动力能源及电池，使全球主要产业，如能源、电厂、电网、汽车、飞机、治金、煤炭及所有机械制造业发生彻底改变，对世界各国工业基础造成剧烈震荡，淘汰及重组现有产业及创造新兴产业，形成新的国际政治及经济局面，所以其影响力不亚于工业革命。此技术为~对干净的冷能源，无任何污染，因此对全球气候变化及环保有重大意义，但人类也可能因此而进入一个新文明。由于新能源发电机体积小、效能高、可提供大量军事用途，如激光、航母、潜艇等。由于新能源发电机只用微小装置去发电，故亦可取代电池。<br />
<br />
&nbsp; &nbsp; 该公司正在为“空间能发生器”技术在中美两国申请12-16个专利，并已经在世界各地制造出几十台实验与工业样机，包括十余种实验样机及不同发电量的工业样机。该产品已可以进入工业化生产。现在正在测试1.2万千瓦的工业样机，将于第三季至四季在美国展示。该公司在6个月到1年内将制造出1万千瓦到100万千瓦的标准机型，其造价不超过传统发电设备，其运行不消耗任何燃料， 仅有一般的机械损耗，可以运行五十年以上。<br />
<br />
&nbsp; &nbsp; 这次展示的“多维空间能发生器”是系列产品中一个初级实验室模型，它将证明从多维度空间获取能源的可行性。<br />
