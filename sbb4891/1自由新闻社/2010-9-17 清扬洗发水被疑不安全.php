													<h2>清扬洗发水被疑不安全</h2>
						
						
																											<div id="postmessage_835095" class="t_msgfont">霸王洗发水含二恶烷可致癌的争议刚刚平息，近日又有消息称联合利华的清扬洗发水被指出存在安全问题，称其采用了比一般洗发水刺激性更大的成分。<br />
<br />
对此，联合利华厂家的相关负责人表示，不能单纯地以铵盐或钠盐判定产品优劣。<br />
<br />
清扬陷入“钠铵之争”<br />
<br />
有媒体报道称，有消费者质疑联合利华的产品“不安全”，因为根据广东省质量技术监督局此前出具的检测结果，在十几款国内外知名洗发水品牌中，大多被检测出含有二恶烷，只有联合利华的清扬例外。因为清扬洗发水使用了另外一种表面活性剂――通常使用在洗洁精等产品中的AES钠盐，钠盐虽不会产生二恶烷，但刺激性更大。<br />
<br />
据了解，AES是一种无毒的产品，由于泡沫丰富，去污力较强，比较温和，性价比高，是日化产品中使用量最大的表面活性剂之一。AES分为钠盐和铵盐，钠盐在日化领域主要用在餐洗中，就是通常的洗洁精。铵盐则用在洗发水、沐浴露、洗手液、洗面奶等产品中。相对AES钠盐，AES铵盐更为温和，AES钠盐碱性较大，刺激性较强。<br />
<br />
厂家称不能以铵、钠判优劣<br />
<br />
对于消费者这一质疑，联合利华相关负责人表示，铵盐和钠盐洗发水，是行业内两个不同的配方体系，目前均已相对成熟。<br />
<br />
此外，AES只是洗发水配方中的成分之一，联合利华的产品在配方上会使各成分有效调节起来。“洗发水中含有二恶烷，铵盐或钠盐之争并非根本，主要与真空脱二恶烷工艺的成熟程度有关。”该人士说。<br />
<br />
对于这一争议，昨日记者从本地几家大型超市了解到，目前并没有接到厂家和相关部门的相关通知。<br />
<br />
文/本报记者 张丹</div>

							
							
							
							
