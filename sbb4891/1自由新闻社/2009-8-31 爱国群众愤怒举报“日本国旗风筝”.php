华商网-华商报讯(记者 孙洪伟)“咋有人放国旗风筝，还是日本国旗!”<br />
<br />
昨日下午，一面酷似“日本国旗”的风筝，飘扬在西安新城广场上空，引发了市民的纷纷议论，还有情绪激动者报了警。<br />
<br />
看到日旗图案 不舒服<br />
<br />
只要天气好，西安新城广场上，每天都有风筝满天飞。<br />
<br />
昨日下午4时40分，在新城广场旁奥罗酒店入住的王先生，从酒店向外张望，突然发现一面风筝，迎着阳光，能够清晰地看到风筝上白底红日图案。<br />
<br />
这个图案，霎时间在王先生脑际闪过：“怎么可能，这是一面日本国旗嘛，太刺眼了!”<br />
<br />
王先生说，不是我对国旗风筝有偏见，可一看见它，我心里就不舒服。<br />
<br />
警察出动 现场劝止放飞<br />
<br />
下午4时45分，王先生拨打了110。两分钟后，西安市公安局新城分局新城广场派出所两名民警就赶到了现场。民警在天空搜寻王先生所说的风筝，并顺着引线找到放风筝的人。<br />
<br />
民警说，在上空看着像日本国旗，当拿到风筝才发现并不是日本国旗。<br />
<br />
记者发现，这面三角风筝，一边白底，上印“红日”;一边黑底，上有月亮，星星图案。从地面迎着阳光看，红日就特别明显。<br />
<br />
放风筝的老者说，这面风筝有象征意味，白天有太阳，夜晚有月亮和繁星，是他自己做的，寓意“日月同辉”。民警还是劝止了该风筝继续放飞。<br />
<br />
对待国旗风筝各有说法<br />
<br />
警察的出动，也引发了广场上市民的议论。<br />
<br />
广场上放风筝的王师傅说，他见过真正的国旗风筝，各国图案都有。他认为即使放飞国旗风筝，也没必要太过惊慌，只要不是别有用心，联想过多反而是一种心理负担。<br />
<br />
也有市民认为，不管是有意还是无意，放飞一些承载有特殊历史感情的国旗风筝，做法欠妥。另一方面，即使放的是我们的五星红旗，按《国旗法》立法精神和规定来看，也是对国旗的不尊重，毕竟放风筝就是娱乐。<br />
<br />
记者在网上查找发现，国旗风筝并不是新鲜事，国内很多风筝制作公司都有过制售。<br />
