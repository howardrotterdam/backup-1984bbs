 <br />
（中央社台北23日电）黑龙江新兴煤矿灾变发生2天后已确定104人遇难，4人下落不明。这起矿灾引起国际重视，一个经济快速起飞的国家却有着如此糟糕的生产安全问题，诚如大陆官员所说，这是「带着血的GDP」。<br />
