													<h2>黑龙江鹤岗新兴煤矿矿难已发现42名矿工遗体</h2>
						
						
																											<div id="postmessage_336651" class="t_msgfont">　　人民网鹤岗11月21日电　（记者曹红涛）记者从黑龙江鹤岗新兴煤矿事故救援指挥部获悉，截至21日17时40分，救援人员已在井下发现42名矿工遗体，66人下落不明。已升井矿工420人，被救矿工中有29人住院，重伤6人，均无生命危险。<br />
<br />
　　据悉，被困人员大约在井下500米的位置。由于井下通风、通讯系统被完全摧毁，救援工作面临不少挑战。目前，事故救援现场共有两个救援通道，一个通道是发生瓦斯事故的通道，另一处救援通道离事发矿600米。目前，事故救援工作已经全面展开，抢险救援指挥部已组织了4支救援队伍参与救援，其中鹤岗区域矿山救护大队150人从凌晨就投入救援，七台河、鸡西、双鸭山三支增援专业队伍160人陆续到达。<br />
<br />
　　事发煤矿距鹤岗市约十公里距离，爆炸发生时，当地居民称震感强烈。记者在事发现场看到，矿井口已完全炸毁，煤矿周围20米范围内，建筑物多因爆炸受损，玻璃全部被震碎。<br />
<br />
　　目前，中共中央政治局委员、国务院副总理张德江已赶赴新兴煤矿事故现场，指导事故救援工作。国家安监总局副局长王德学、黑龙江省委书记吉炳轩、省长栗战书、副省长王玉普已赶赴事故现场，指导抢险救援工作。<br />
<br />
　　背景资料：黑龙江龙煤集团鹤岗分公司<br />
<br />
　　黑龙江龙煤集团鹤岗分公司新兴煤矿为原国有重点煤矿，高瓦斯矿井，该矿生产能力145万吨/年。鹤岗分公司是经黑龙江省人民政府批准的由鹤岗矿务局改制注册的国有独资企业，是520家国有重点企业之一。现有职工88320人，技术人员1390人，总资产 65.1亿元，年产商品煤1200万吨。鹤岗矿区已有84年开采历史。</div>

							
							
							
							
