																											<div id="postmessage_338354" class="t_msgfont"><a href="http://news.163.com/09/1123/13/5OQC1A1K000120GU.html" target="_blank">http://news.163.com/09/1123/13/5OQC1A1K000120GU.html</a><br />
2009-11-23 13:12:45　来源: 中国新闻网(北京)　 手机看新闻<br />
<br />
核心提示：23日，黑龙江鹤岗新兴煤矿&quot;11・21&quot;矿难遇难矿工赔偿方案出台。方案共有两套，其中家属若选择一次性赔偿方案，最高可获赔30.26万元。<br />
<br />
中新网鹤岗11月23日电 23日，鹤岗新兴煤矿“11・21”矿难遇难矿工赔偿方案出台。方案共有两套，其中家属若选择一次性赔偿方案，最高可获赔30.26万元。<br />
<br />
该方案由鹤岗新兴煤矿“11・21”矿难事故处理组制定，依据为国务院《工伤保险条例》第375号令及企业相关规定。<br />
<br />
据介绍，第一套方案中规定，丧葬补助金为6个月的统筹地区上年度职工月平均工资，鹤岗市为1900元，此项金额总计1.14万元。一次性工亡补助金为48个月的统筹地区上年度职工月平均工资，此项为9.12万元。此外，供养遗属抚恤金为配偶每月708元，其他遗属为每人每月531元。孤寡老人和孤儿每人每月增加10%。<br />
<br />
此方案中规定，领取供养遗属抚恤金的遇难矿工配偶应是无生活收入的，子女则应未满18周岁，方案中对于赔偿金的分配方式和遗属范围等都作出了明确的规定。<br />
<br />
另外一套方案是一次性赔偿方案。包括一次性遗属赔偿金20万元，一次性工亡补助金10.26万元。若遇难者家属选择此套方案，则不再享有供养待遇。<br />
<br />
据介绍，赔偿方案中还有一条特殊政策，若家属在签订赔偿协议后24小时内出殡，则可额外获得3万元补助，若48小时内出殡，可获2万元，72小时出殡，可获1万元。<br />
<br />
目前方案已通知到遇险矿工包保组，工作人员将分别到每一户遇难矿工家中进行方案的详细解释，由家属作出选择。(完）<br />
