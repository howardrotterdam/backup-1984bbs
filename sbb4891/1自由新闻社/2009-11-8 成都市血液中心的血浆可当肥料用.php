“看看我的肥料哈！高蛋白的营养品哦！”11月7日，在某网络论坛中，名为“来来回回”的网友图文并茂地描述了他用血浆兑水浇灌兰花的过程。网帖照片中，在血浆袋上可以清楚地看到“成都市血液中心”字样，以及血浆编号、献血者编号、采血日期（2009．10．03）和失效日期（2014．10．03）等信息。论坛资料显示，该网友来自四川成都。 <br />
<br />
该网帖迅速在国内一些知名网络论坛和网站传开。一些网友认为，用血浆浇花让广大无偿鲜血者心寒，是对生命的不尊重；还有网友对血浆来源表示关注。 <br />
<br />
成都市卫生局主管宣传的甄英处长10日下午向新华社记者表示，现已查明该血浆是由成都市血液中心发到某县级储存血库，随后再转至本地一家工矿医院。“问题出在该工矿医院血液报废管理程序上，输血科值班人员发现血液有絮状物后，进行报废处理时没有按规定处理，而是交给科室另一人员传递给了其朋友。”甄英说。 <br />
<br />
甄英说，成都市卫生局知悉网帖情况后，迅速责成血液中心、执法大队及相关处室，对事件进行联合调查。目前，成都市卫生局已对有关责任人进行处理，对输血科值班人员解除劳动合同，并对科主任予以解职，传递者给予了留用察看处分。 <br />
<br />
刚刚在2周前无偿献血的四川大学2006级工业设计专业学生戴恒男说:“我们去献血是为了帮助别人挽救生命，知道‘血浆浇花’事件后我感到非常难受，但我今后还是会继续去无偿献血，因为我相信这样的事情只是个别事件。”</div>

							
							
							
							
