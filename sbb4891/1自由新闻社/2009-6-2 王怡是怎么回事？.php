													<h2>转：[四处回响]最佳传球：理解王怡――思想、政治、宗教、文学的王怡</h2>
						
						
																											<div id="postmessage_107844" class="t_msgfont">王怡 发表于 2006-10-18 12:26:53&nbsp;&nbsp;<br />
按：看过很多品论我的文章。这一篇前几天在关天看到，忍不住还回了贴，结果第二天就发现被删了。今天在羽戈那里又发现。在关天和闲闲贴了几篇文章都被删了。看来最近是只准批评不准表扬了。很感谢最佳传球对我的理解，赞扬和批评混在一起，很多评点令我深受启发。今天上午起来读《耶利米书》，读到“小偷被拿的时候，怎样羞愧，你们在耶和华的面前，也照样羞愧”（大意）。作为一个知识分子，在这样的读者面前，也是照样羞愧。――2006-06-13 “王怡的麦克风（第4版）”<br />
<br />
【理解释意】<br />
　　<br />
　　过去，当王怡被人称为“网络意见领袖”，宣讲“知而不言是罪”的时候，我对他很不以为然。那时我也常上网，偶尔也表达一些自己的意见，但我从不认为需要有谁做我的领袖，我的表达只与我自己有关，同样王怡的表达也只与他自己有关，谁都不代表谁，谁也不领导谁。<br />
　　<br />
　　王怡表达自己的意见时敢想敢说，常令我自叹弗如，我知道他比我勇敢，但斗士是斗士，犬儒是犬儒，高度不成比例，二者间肯定就搭不起领袖的桥来。因此，我认为那些赞誉王怡为“领袖”的犬儒和斗士们，也不过是在拜偶像，带有世俗的奴性味道，他们正在不自觉地把王怡神圣化。当然这种奴役永远都是相互的，人接受领袖的奴役同时，肯定也对领袖还以奴役。造神运动算是中国人的一项传统习俗，我对此并不感到不理解。<br />
　　<br />
　　我不能理解的其实是王怡，他自称是一位自由主义者，而自由与奴役犹如水火般不相融，他怎么就泰然接受这个违背其信仰的称谓，不站出来反对呢？由此，根据世俗的经验，我便武断他的信仰自由主义必是假的，他高呼自由也许别有所图。任何不了解王怡，但了解中国人性的人都很容易根据“常识”得出这样的判断，恰如鲁迅先生所说，是可以不忌以最深的恶意去度中国人的。<br />
　　<br />
　　我在用恶意度王怡的同时，也还替他怀有一丝担忧的，称颂和赞美固然可以享受，但在中国，毕竟领袖和圣人都不是很好当，人稍不留神，身上的世俗有限性就有可能不慎显露，那么被先前那些簇拥自己上神坛的人给揪住，自然免不了迎来一顿义愤填膺的暴打，而且打人者还会个个委屈异常，失望和痛心之情溢于言表，仿佛被打者给他们虔诚的心灵造成了莫大的伤害。所以，我认为真正的自由主义者肯定都不屑于当领袖，当圣人的，原因很简单，领袖和圣人也是对当事者的一种奴役，既然王怡那时没表达自己对这种被奴役的不满，我自然推论他是个假的自由主义者。<br />
　　<br />
　　后来的王怡果然因为“公共知识分子”和“意见领袖”的某些词不达意，遭到人们的许多批评和辱骂。再后来，他又置诸多他的期许和崇拜者们于不顾，毅然投向了上帝的怀抱，使得身后的失望和批判声此起彼伏，一时间遽成高潮。这时，既未期许过，也未崇拜过王怡的我，却忽然感觉对他有了更深的理解，明白了从前的不以为然有些草率，他不言不满，多少和不言有关，未必就是满，而投身上帝的王怡，已用行动很好地表明了他对加诸于其身的领袖和偶像等世俗称谓的弃绝，他在上帝的无限里照出了自己的有限，从而也得以摆脱了世俗名利的奴役，重新获得自由的他，仍得以继续行他的自由主义者之路了。<br />
　　<br />
　　但很多人并不这样看，他们认为这是王怡的堕落，于是失望和愤慨之情溢于言表。在他们看来，既然是领袖，就不该私自皈依什么宗教，领袖是没有宗教信仰自由的；同时，人们把你像圣人一样期待，你也没权利犯下世俗的过错，否则人们就有理由义正严辞地斥责你原来的面目为虚伪。基于对此时王怡的好奇，我花了一个晚上的时间，登陆他的麦克风博客阅读他近期的文章，尤其是关于政治神学的一组文章，感悟颇多。我还有很多读不懂之处，也有很多不赞同的内容，但在一个大境界的思想背景帮助下，对他的理解还是觉得比过去明晰了许多。<br />
　　<br />
　　当然，一个人对另一个人恰如其分的完全理解永远是可望不可求的，所谓理解，不过是用自己的镜子去映照别人，它提供的也只是一种投向对方的特别视角，所以很多时候，获得理解的只是自己，而非别人。幸好中文词汇都不太讲时态，所以我这里大言不惭地说“理解王怡”，如果阅者和王怡本人感觉不以为然，大可以取其“理解”之将来时涵义（意愿），而避其完成时涵义（效果）。这样，我就可以地无所顾虑地说出我的理解了。<br />
　　<br />
　　【思想的王怡】<br />
　　<br />
　　王怡首先是一个思想者，思想是他生存的一种基本形态，也是他称自己是知识分子的一个有力根据。只有知识的人算不上知识分子，除了知识，人还必需要有思想。&nbsp;&nbsp;<br />
　　王怡的知识主要源于他的广泛阅读，这从他的写作中就能够看出来，他的文章总是旁征博引，纵横于古今圣贤，是典型的“六经注我式”风格，但注的范围则远远超越了六经的局限。六经注我，是典型的诗性思想者的特征，这也是我理解王怡时的一个发现，虽然他具有很强的理性逻辑思维能力，但就本质的思想类型而言，他仍旧是一个感性之人，身上有着浓郁的诗人气息。<br />
　　<br />
　　王怡的诗性特征应该主要来自于生活，而非来自书本。据说他是一个很究讲生活情调的人，我这里所说的生活情调，是指一种审美的生活方式，也就是惯常所说的诗意地栖居。在诗性的王怡看来，生活就应该是美，美也应该是生活，二者即便不能一直统一，至少也不该一直割裂。但遗憾的是，我们的社会生活呈现的恰恰是美的严重缺失状态。<br />
　　<br />
　　生活中为何缺失美？这种潜在于内心的提问自然会触动诗性王怡的思考，随着他将这种思考逐步深入拓展开去，于是探询生活与美的诗性的王怡，就渐渐变成了一个习惯于理性和逻辑思考的理想主义的思想者。如果把理性（逻辑）比作故态的冰，把诗性（感性）比作液态的水，那么就可用一个蹩脚的比喻来加诸王怡：他于是变成了一颗愤怒的冰雹。对于冰雹来说，它不惧怕寒冷，但却受不住热情，遇到残酷的寒冷，它会还给你更加寒冷的坚硬，但倘若遭遇的是热情，它的坚硬便会自行融化，并最终露出水样的柔情。<br />
　　<br />
　　作为学者的王怡，他身上的这种诗性特征，很容易遭到学院派学术标准的诟病。在一般人看来，诗性和学术，是思想方式的两个端点，如果两个端点融合一处的话，思想的阐述就成了不伦不类的缠绕，也将会同时不能见容于诗人和学者两个界限明晰的阵营。<br />
　　<br />
　　多年的理性生活习惯，使得王怡渐渐以为自己拥有了可以信赖的理性功能，所以他对自己的评价，更倾向于学者，而非诗人，但理性的外壳建筑得再厚重，也无法掩盖他感性的思想内核，于是不经意间显露出的诗性方式，便常常成为他学术表达的软肋。一方面，人们认为他意气风发，才华横溢，拥有卓越的学术天分，另一方面，人们又总觉得他缺少些什么，觉得他跟周围那些学者并不等同。其实不是缺少，而是多出，他多出了学术工作的最大天敌：诗性。<br />
　　<br />
　　所以在我看来，王怡即便学术功力再浓厚，也难以成为世俗意义的合格学者，除非他对自己的理性包装彻底覆盖住自己内核深处的诗性，但这意义又何在呢？世间多出一个学者，却少了一个王怡。<br />
　　<br />
　　【政治的王怡】<br />
　　<br />
　　王怡身上有一种蓬勃的政治热情，他是一个执着于把思想化成行动的人，尽管在思想上他堪称是典型的理想主义者，在行动上却是一个不折不扣的现实主义者，行动上的现实主义也是他跻身公共领域的一种看得见的形态。当执着于看得见的理想时，思想的王怡就变成了政治的王怡。<br />
　　<br />
　　跻身政治的王怡身披的是一件自由主义的外衣，之所以说自由主义是他的外衣，是因为任何主义对人来说，都是外衣，都可以脱掉的，自由主义也不例外。但文明社会的公共领域一般不会容许一丝不挂的野人参与进来的，进来的人都必须得穿上衣服，所以在我看来，政治就是一种必须穿上衣服的生活，是人类所谓文明进步的一种标志，不过人在穿上衣服之后，智慧常常会受到损害，开始不自觉地以衣服来鉴别人，便容易忽略其实衣服本来都是可以脱下来的附属物，而当人赤身裸体时，每个人的身份地位都会恢复原形，恢复同等，人与人就不会产生那么多无聊而残酷的政治斗争了。好像有些扯远了，离开王怡，说起卢梭了。<br />
　　<br />
　　尽管王怡自称是保守主义的古典自由主义者，但他肯定不会认同卢梭的那种保守的，比起卢梭的保守，王怡显然要文明进步得多，所以王怡是一个抬头向前看的乐观自由主义者，对人类（中国）的美好似乎未来充满信心。信心成就了他的热情。<br />
　　<br />
　　过去的王怡几乎通读自由主义的所有著作，并从中揉出自己的政治方案：宪政主义。倡导宪政是看得见的思想者王怡手中看得见的武器，他籍此来入世，但平心而论，不管他的宪政理论有多么现实可靠，都依然是种诗性人格的政治表现方式。和王怡同样的这种诗人前辈，远者有堂・吉诃德，近者有毛泽东，前者从骑士小说里揉出了自己的入世方案，然后踏上征险之路，去改造世界，后者从马列著作里揉出了自己的革命思想，亦走上另一条征险之路，去改造世界，不同的是，前者在小说里到处碰得头破血流后失败了，后者却杀开一条血路获得了成功。王怡身上的诗性特征和那两人很近似，惟一的区别是他不承认自己有一样的血性，在他的理解中，他手中的武器不是用来战斗的，而是用来和平的。&nbsp;&nbsp;<br />
　　宪政自由主义的经典著作，与骑士小说和马列著作并没有什么本质不同，倘若人坚持认为人可以通过自身努力成为圣贤的话，成为圣贤，人就可以不犯错了，那么政治选择什么模式，都将只有是，没有非。这种惟是的信念可以为人带来行动的足够信心，参与行动的人也将对自己行动的结果持有足够的乐观。王怡尽管皈依了上帝，承认了自身的有限，但他现在仍对自己的政治理念充满乐观的自信，虽然他一再引用《圣经》里的话说，“上帝的归上帝，恺撒的归恺撒”，但他的行动却分明在努力使恺撒与上帝结成统一的联盟，而他身上恺撒式的热情显然更浓一些。<br />
　　<br />
　　【宗教的王怡】<br />
　　<br />
　　王怡投身于上帝，是被很多人不理解，甚至极度反感的事，甚至很多人认为他不过是在借上帝的名，行自己的事，王怡在投身于宗教后所写的一组《基督教政治哲学札记》也恰好证明了这一点。但至少我个人丝毫不怀疑他的宗教诚意，而且在我看来，跨入宗教之门，恰是王怡本质人格的正常体现，一颗温善且向往诗性的心灵，只要自由敞开，是最容易听到上帝召唤的声音的。<br />
　　<br />
　　但严格地说，王怡现在的宗教性其实非常薄弱，对宗教理解的似是而非，甚至解不达意之处，在他身上不一而足。这与他用理性掩盖心灵的多年学者生活有关，也有他趋向上帝的具体路径有关，当然也与从小就接受的强大无神论宣传有关。一个人心灵敞开的程度，决定了他与上帝之间的距离，毫不客气地说，天性具有宗教性的王怡，多年来选择的生存方式是封闭心灵的，他也许一开始希望用敞开大脑的方式敞开心灵，但理性思维的过度敞开，最终导致了他对自己心灵之门的压抑。<br />
　　<br />
　　至今，王怡的宗教性仍处于一种不坚固的萌芽状态，他对政治神学的热衷表明他所走的是一条由理性趋向神性的路，这是一条克尔恺郭尔式的道路，就宗教意义来说，它其实是违背宗教性的。舍斯托夫就曾经直接批判过克尔恺郭尔的这种方式，他反而认为宣称上帝死了的尼采比克尔恺郭尔具有更为真实意义的宗教性。但就中国的现实来说，由理性趋向神性的道路，或许也是最正常、最适合的道路，包括我本人在内的许多人，都不得不选择这样一条辗转谬误的道路。此路漫漫，其修远兮。<br />
　　<br />
　　尽管过多的理性蒙蔽了王怡神性心灵的呼吸，但我依然不怀疑他宗教性的坚定，他是那种具有感悟神性品质天然能力的人，这样的人只要体验到一点神性的力量，就断没有回头走路的可能，除非他完全丧失掉自己的本质，彻底成为一个世俗的政治之人，否则心灵不死，心灵之门肯定就会一直向上帝永远敞开的。<br />
　　<br />
　　【文学的王怡】<br />
　　<br />
　　我有种猜想：倘若王怡不是生在中国，那么他也许会只成为思想的王怡，只成为宗教的王怡，并最可能成为文学的王怡，很少可能会成为政治的王怡。<br />
　　<br />
　　王怡的文字写作能力最为令人称道，他的文章不但旁征博引，纵横古今，更是理性意味浓厚，某些字句往往直抵心灵，令阅读者赏心悦目。我没听过他的演讲，但看过他的几篇演讲词，和写作风格差不多，口才应该不一般。口才好是文学的王怡的最大敌人，因为对于文学作家来说，真正的使命是写，而不是说，好的口才是政治家和社会活动家的宝贝，文学作家则完全不需要。文学作家的最佳楷模是卡夫卡和福克纳，而他俩均是不善言词的“木纳”之人（尤其福克纳）。<br />
　　<br />
　　文学作家既需要天赋的诗性，也需要良好的理性，更需要获得卓越的神性，同时还要具有非凡的观察力、洞察力、记忆力和想象力，以及表达的勇气和叙事的技巧，包括运用自如的文字能力，当然勤奋和甘于平庸的世俗生活也同样不可或缺。<br />
　　<br />
　　王怡具有文学的天赋这一点显然不言而喻，但就目前看来，他似乎缺乏对文学的诚意，骨子里似乎没有写作永恒作品的野心，所以这里大谈特谈文学的王怡纯属是我故意跑题，这也是我个人的局限造成的，毕竟相对于革命式的堂・吉诃德，我肯定更热爱并尊敬文学式的桑乔。<br />
