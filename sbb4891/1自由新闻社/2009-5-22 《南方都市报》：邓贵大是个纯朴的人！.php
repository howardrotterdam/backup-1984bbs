													<h2>《南方都市报》：邓贵大是个纯朴的人！</h2>
						
						
																											<div id="postmessage_99347" class="t_msgfont">看惯这世界无耻的嘴脸，对狰狞已经有了熟悉的适应力，但南都2009年5月20日的无耻，让自己再一次悲哀地感到，失望的泡沫就是从信赖的背后挤破，看了今天南都A12-13版的《女服务员与招商办官员的致命邂逅》，看到这一惯敢言出名的报纸、貌似站在人民当中的立场，以一种小说的手法，揉和传播学和心理学技巧的大作，不禁感到，无耻往往就在你身后，带着一脸通俗的微笑，去用文字的技巧来愚弄你。 <br />
<br />
&nbsp; &nbsp;众所周知的是，报纸与小说周刊唯一的区别就是：报纸就是客观报道事实的，所以在南都新闻A12-13新闻专版的内容，人们都是当新闻是读的，而不是在新闻的版面写小说。&nbsp;&nbsp;所以这篇一开始就使用心理学人格化的技巧，以被杀邓贵大老婆哀婉的视角：邓贵大被杀死的那天，醒来得很晚。早上7点，郑爱芝从床上爬起来，看到丈夫连一个翻身的迹象都没有，平常这个时候，夫妻俩几乎是同时起床，一起吃早餐，然后各自忙碌。但那天是周日，郑爱芝以为这是由于丈夫头一天晚上应酬太累，疲惫还未褪去，所以也就没吵醒他。‘那天很奇怪，他是个生活有规律的人，晚上从来没有超过12点睡觉，早上也没有迟过8点起床。’她后来回忆说，当时并没有将这点视为不祥之兆。大概10点半的时候，郑爱芝从她工作的地方―――野三关政府宾馆回家取钱买高压锅，她在宾馆食堂上班，那里离家只有一墙之隔。她看到丈夫蹲在院子里，正在跟一个老太婆聊天。郑爱芝上前问她是谁？“你不知道么？四楼的老人家，李某某的母亲。”邓贵大说。他们以夫妻俩一贯的无关痛痒的方式聊着，郑爱芝出门时叮嘱说，‘你们一天在街上跑，今天星期天，记得把电费交上。’”&nbsp;&nbsp;<br />
<br />
&nbsp; &nbsp;在惜墨如金的新闻面里，文章居然有了这么多的篇幅来描述死者平凡而可触摸的生前的一天，突出了他的平凡、朴实甚至平庸，淡化了事件发生时候飞扬跋扈的场面，平息了人民对贪官污吏的愤怒。新闻的一开始还用一连串的问句貌似公允地问道：“5月10日到底发生了什么？被刺死的官员―――身高1.60米，体重不到45公斤的邓贵大，在很多人眼里，是举止温和的一个人，为何当天判若两人？而邓玉娇在刺出水果刀之前，又经历了什么？” 请问，刻意突出这1米6身高45公斤的弱小形象，这与案件有什么关系？&nbsp;&nbsp;大多残忍的杀人犯都对家里很柔情，但这一点也没有耽搁杀人犯对外人的狠毒。 <br />
<br />
&nbsp; &nbsp;而对于用钱抽打邓玉娇脸的事件是怎么解释：“到后来，警察以遗物方式归还了4000多块钱，那正是击打邓玉娇头部的一叠人民币。郑爱芝说，邓贵大平常爱好打牌，兜里面总装着几千块。”看到没，这不是公款吃喝，这是一般百姓爱打牌在口袋装的打牌钱。&nbsp; &nbsp; <br />
<br />
&nbsp; &nbsp; 与此同时，文章同样描绘了邓玉娇出事的那一天：“邓玉娇在几个朋友中长得最俊俏。她的穿着，对于小镇上的人来说，过于时尚；尤其是4月份，她从浙江游玩回来后，几乎每天戴着一副红框的没有度数的眼镜。她对姐妹们说，“这副眼镜花了300块钱，很好看。”但她走路又是大步流星，无拘无束，这在很多人看来，显得格外张扬，乃至有些怪异。”这是伏笔，衬托出邓玉娇的飞扬跋扈，在群众中搞坏她的想象，这方面描写太多了，还有：“事实上，几乎所有朋友，都察觉出了异样。唐芹说，她真的是精神出了问题。这一对好了几年的朋友，最近一个月里危机频频：邓玉娇脾气稀奇古怪，很暴躁，谁也不搭理。除了坎坷身世和不幸遭遇，爱情对于邓玉娇来说，也很残忍。这些都是邓玉娇的亲人们所不知情的：在不到两年的时间里，她经历过两段无疾而终的感情。第一次是在去年，当她发现男朋友原来是有家室的，她被欺骗后，更加抑郁。” <br />
<br />
&nbsp; &nbsp; 我们可怜邓玉娇的母亲张树梅被媒体不良诱导的大嘴巴，给自己女儿日后的定罪打下很多伏笔，有时候对这些可怜愚昧而又自以为是女人，就一个感觉，是“哀其不幸、怒其不争！”，也许张树梅自作聪明以为说自己女人抑郁症，就不会被追究法律责任，但毕竟抑郁症的烦躁杀人，与精神病的不可自控杀人有本质区别。下面的话，我不相信是一个母亲所说的，我想不通一个母亲会说这些让人产生预谋杀人联想的话：张树梅听了半天，还是不理解：“家里有水，怎么跑到这里洗衣服？” <br />
<br />
&nbsp; &nbsp;然后我们再看看记者对现场的“细腻”描述：“杨红艳赶到的时候，休息室内的时间仿佛凝固了。一个玻璃杯在打斗中掉在了地上，邓玉娇瞪着眼，咬紧牙，紧紧握着一把水果刀，“她被激怒了。”杨红艳说。还没来得及打招呼，杨红艳看见站在邓玉娇对面的邓贵大迎面扑下来，倒在了邓玉娇脚下。“放下刀，”杨红艳对她喊，“你还拿着刀干嘛？”邓玉娇仿佛什么也没听到。杨红艳准备拉她出去，邓玉娇置若罔闻” <br />
<br />
&nbsp; &nbsp; 然后记者恰如其时的煽情来了：“邓贵大下葬时，读高二的儿子回来了，“你应该知道，你爸爸不是这种人。”她对儿子说，但心里又认为，总归是件丢人的事。” <br />
<br />
&nbsp; &nbsp; 这样，记者还不忘同时踩邓玉娇一脚：“除了坎坷身世，爱情对于邓玉娇来说，也很残忍。在杀死邓贵大前半个月，她从浙江回来，发现男朋友电话再也打不通，她变得歇斯底里，数次在朋友面前失声痛哭；杀人当天的中午，邓玉娇在几个姑娘的麻将牌局中和一个朋友吵了起来；起因是那个叫唐芹的女孩劝她：‘你以后走路可不可以不要那么张扬？’‘关你什么事，’邓玉娇很气愤，边说话，边拍打着桌子，一如她平常的个性。朋友们不再理会她，退回到了里面的卧室。平息下来后，她主动进去说，‘你们也知道我的脾气。’随后，她加入了姑娘们的“麻将战”。” 果然，记者用妙笔把邓玉娇刻画成一个低俗、脾气暴躁和专横的不讨人喜欢形象。 <br />
<br />
&nbsp; &nbsp; 我想，此时南都的记者，你猥琐地躲在群众当中，用貌似客观的陈述，以人格化的心理学技巧，用小说般渲染铺垫的手法，终于选择了出击的时机，弥补了一辈子的时差，把握了一生中最大的一次际遇。等待一个巨人倒下的砰然声音时，我想他一定是用眼蒙住双眼此刻心在怦怦地跳，身体在颤抖着，正如所有的小人在背后捅了人一刀随后那段时间的漫长和期待。 <br />
