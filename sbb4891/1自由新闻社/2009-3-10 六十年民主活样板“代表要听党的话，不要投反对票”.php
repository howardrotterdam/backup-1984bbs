																											<div id="postmessage_49317" class="t_msgfont">“最老”的人大代表申纪兰：55年来,我从没投过反对票<br />
<br />
<img src="http://epaper.nddaily.com/A/res/1/523/2009-03/10/AA06/res01_attpic_brief.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　申纪兰参加1959年-1964年第二届全国人大会议时的照片。CFP供图<br />
<br />
<strong>　　申纪兰</strong><br />
　　<strong>年龄：</strong><font face="楷体_GB2312 ">80岁</font><br />
　　<strong>身份：</strong><font face="楷体_GB2312 ">全国人大代表、山西省长治市人大常委会副主任、山西省平顺县西沟村党总支副书记。</font><br />
<font face="楷体_GB2312 ">　　从1954年第一次骑毛驴到长治市，坐敞篷车辗转到太原，转火车到北京参加全国人代会，全国人大代表、80岁的申纪兰，曾经跟毛主席握过手，跟周总理吃过饭，被江泽民称做“凤毛麟角”，她也是中国惟一从第一届连任到第十一届的全国人大代表。她说55年来“从没投过反对票，对党对国家一直拥护”。</font><br />
<br />
　记者（以下简称“记”）：有人说你第一次来京开会是坐毛驴来的？<br />
　　申纪兰（以下简称“申”）：是啊。我们平顺县西沟村到长治有70里地，要走7个小时还到不了。原来出县没有路，只有羊肠小道。我先坐毛驴来到长治，倒了个车，是那个敞篷的，到太原，再倒车，要倒四五次车，再从太原坐火车到北京。走了4天。<br />
　　记：记得第一次来开会穿的啥衣服不？<br />
　　申：那时候农民没有衣服穿。省里给我们人民代表做了一身崭新的蓝卡其布衣服，国家才造出来的料子。说我们代表群众去，不能穿得不好。要去见主席，都激动。<br />
　　记：那给主席带礼物了吗？<br />
　　申：没有，没想到要带啥。我们来就是带着群众的利益，来向毛主席汇报来了。<br />
　　记：那一年见到主席了吧？<br />
　　申：我们都想着见毛主席，有的人说，见了毛主席，死了也不后悔了。开完会，主席接见我们，我们跟他照了相，握了手。我们都感动得眼泪都在眶子里把眼都蒙住了，看不清毛主席的脸，只看到下巴上的痣，只觉得毛主席的手很厚。大家都抢着跟主席握手，好多代表都没握上。<br />
　　记：你当年是怎么选上的呀？<br />
　　申：我是从基层投票选出来的，然后在山西省里选出来。那时候我领导妇女，追求平等解放。实现了全国第一村子男女同工同酬。全国妇联在我们那里考察，人民日报也到那儿去过。<br />
　　记：说你还到周总理家做过客？<br />
　　申：那是1958年，我到北京参加全国妇女群英会。全国七个女社长到紫光阁与周总理座谈。谈了3个小时。我们都很高兴，还跟总理一起吃了饭，他敬了我们一杯酒。多感动人。总理当时问我小麦产量多少，我说3000多，他问多多少，总理不糊涂，他要准确数，我不敢说，那时候浮夸风。只说个大数。我们还跟总理合了影，他在我们本子上签了字，本子我现在还留着。<br />
　　我还记得总理最后一次念政府工作报告，总理病了瘦了好多。最后报告都没作完。大家都拼命鼓掌。总理坐下去又站起来，坐下去又站起来。大家都流泪了。<br />
　　记：你还见过江泽民、胡锦涛？<br />
　　申：我记不得是哪一届，江泽民到我们分组审议的会议室。他说我“很罕见，凤毛麟角”。胡主席和朱F基都到过我们村。<br />
　　记：现在代表们表决的方式跟过去不一样对吧？<br />
　　申：两码事，过去是举手，现在是按。<br />
　　记：你过去有不举手的时候吗？<br />
　　申：我非常拥护共产党。当代表就是要听党的话，我从来没有投过反对票。<br />
　　记：大家为什么这多年老选你当代表呢？<br />
　　申：我一直代表农民的利益，跟农民在一起，他们有困难就找我，我就帮他们信。我当了干部还跟农民在一起，还劳动。我还种地。我几十年都不谋私利。全心全意，跟党保持一致。跟中央保持一致。</div>

							
							
							
							
