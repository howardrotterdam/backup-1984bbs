																											<div id="postmessage_49667" class="t_msgfont">定格人民大会堂第一声“我反对”的瞬间<br />
<br />
 <a href="http://www.huanqiu.com" target="_blank">http://www.huanqiu.com</a> 来源：中国青年报<br />
<img src="http://himg2.huanqiu.com/attachment/090305/829fc96aad.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　照片:1988年3月28日，人大代表黄顺兴在七届全国人大一次会议上发表反对意见。本报记者 贺延光摄<br />
<br />
<br />
　　“这是全国人大代表第一次公开说，‘我反对’。”中国青年报社图片总监贺延光笃定地说，“如果我也像国内其他记者一样早走5分钟，那就错过了这张照片。”<br />
　这张黑白照片发表在1988年3月29日《中国青年报》一版。照片旁边有本报记者麦天枢、马役军对全国人大代表黄顺兴的专访:《我为什么投反对票》。<br />
<br />
<br />
　　2008年12月4日，贺延光回忆:“那是1988年3月28日，人民大会堂在开七届全国人大一次会议，这天的议程是选举专门委员会。我走进会场，看见走道上每隔一二十米就有一个竖立的麦克风，不知道有什么用。”<br />
<br />
<br />
　　贺延光说:“投票选举教科文卫委员会成员时，我正坐在主席台附近的地板上，那天有点儿发烧。突然，一位人大代表走到话筒前，大声说:‘我反对！’会场静了一下，他发言完毕，全场响起热烈掌声。”<br />
<br />
<br />
　　现场发表反对意见？！这是全国人民代表大会自1954年召开以来，第一次公开出现的不同声音。贺延光说:“我拍下了这一刻。在现场不知道他叫什么，赶紧打听，后来知道他叫黄顺兴，来自台湾代表团。”<br />
<br />
<br />
　　“当时为什么很多国内采访‘两会’的记者都走了？我认为:一是时间快到中午；二是记者们知道，这种投票出不了新闻。别人批评我们的人民代表大会是‘橡皮图章’，一些跑过两会的记者根据经验，在大会进行到表决流程时就提早离开了会场。”<br />
　　今天，贺延光说:“你看照片上，围在黄顺兴旁边的记者，大多是来自港澳的。”<br />
<br />
<br />
　　当时，贺延光掉转镜头，拍下了主席台上的邓小平、杨尚昆等中央领导，他们都在认真倾听黄顺兴的反对意见。<br />
<br />
<br />
　　当晚，中国青年报一位值夜班的编辑兴奋中用带点可惜的语气对贺延光说:“你要能把邓小平和黄顺兴拍到一张照片里去，就更牛了。”贺延光说:“怎么可能呀，根本就不是一个方向嘛。”<br />
<br />
<br />
　　黄顺兴在发言中，解释了他反对的理由。候选人他不认识，但从简历上看，（教科文卫委员会）主任委员人选已经89岁高龄。他说:“这么大岁数的人，不应该再辛劳他了。难道就没有年轻人为国家做事？”在第二天的报纸上，通过的名单里，主任委员是周谷城。<br />
<br />
<br />
　　今天，有记者问贺延光:“知道投反对票的代表来自台湾后，有没有想过大陆的代表也能更尽责一些？”<br />
<br />
<br />
　　他说:“那次会上，也有大陆代表投反对票。我当记者以来，1988年的两会是最开放的一次。<br />
<br />
<br />
　　会上还有故事:全国人大代表红线女给在主席台上吸烟的邓小平递条子，提醒他不该当众吸烟。邓小平看后把烟灭了。”<br />
<br />
<br />
　　投反对票的黄顺兴，早年在日本学习农学。回到台湾后，在国民党戒严时期，以无党派的身份当选为台东县县长，非常不易。后来在台湾的立法机构里，担任两届“立法委员”，因敢于直言得名“黄大炮”。他创办了台湾的第一个环保刊物。<br />
<br />
<br />
　　1985年，黄顺兴从日本回到大陆后，担任全国人大常委。在1988年的人大会上，他提议设立代表“秘密投票处”。因为投票的代表座位挨得很近，投票的结果邻座都能看到，侵犯了代表的权利。他的建议立即被采纳。<br />
<br />
<br />
　　1992年，全国人大会上表决三峡工程时，他按规则要求发言，但没有获准，他因而退席。后来他说:“大水又不会淹到我台湾去。我是站在全国人民的立场上说话。”这年，全国人大代表的反对票，弃权票都创造了新纪录。1993年，他辞去全国人大常委并一度返回台湾。<br />
<br />
<br />
　　贺延光最后一次见到他是在2000年。在黄顺兴位于北京北太平庄“部长楼”的家里，贺延光看到自己拍的那张照片，挂在老人总能看到的地方。<br />
<br />
<br />
　　两年后，黄顺兴因心脏病在北京逝世，享年79岁。在八宝山革命公墓举行告别式后，他的骨灰被儿子带回了台湾，遵其遗嘱，撒在大海里。<br />
<br />
<br />
　　自黄顺兴开始，越来越多的人大代表开始依法代表人民说“不”。2001年，沈阳市人大代表投票否决了市中级法院的报告。这在中国各级人大会议的历史上是第一次。中国宪法学会会长韩大元对本报记者说:“这是中国民主政治的标志性事件。”<br />
<br />
<br />
　　2008年3月，记者贺延光又一次参加了全国两会的报道。香港《大公报》在今年3月18日说:“从1988年黄顺兴代表投下全国人民代表大会有史以来的第一张反对票，到如今一些决议表决中出现的500多张反对票和100多张弃权票，20年时间，人大代表们用选票表达意愿的能力日趋提高，民众的声音越来越响亮。”<br />
<br />
<br />
　　今天的互联网上，有很多用简体或繁体字写的纪念黄顺兴的文章，其中一篇的标题是:《不应忘记的台湾省人大代表:黄顺兴》。 （记者 王尧）<br />
