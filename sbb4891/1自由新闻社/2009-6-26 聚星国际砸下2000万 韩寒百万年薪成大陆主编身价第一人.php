<br />
　　据透露，韩寒已向名人好友发出约稿、开专栏的邀请。对于这本112页的杂志来说，韩寒除了主编的身份外，也是作者之一，其最新的创作，如杂文和小说都将在杂志上第一时间发表。但韩寒在自己杂志上写稿，将不再另外领取报酬。<br />
<br />
　　发行30万册不是问题 <br />
<br />
　　对于30万册的首印量是否会造成库存积压，投资方表示并不担心。“虽然需要支付天价稿酬和巨额的主编薪酬，但预计韩寒的杂志第6期就能够实现盈利。”负责人称，杂志的盈利模式，除了看好杂志本身的发行之外，还有部分将来自广告收入，“韩寒的号召力，目前已吸引了多家广告商前来洽谈”。<br />
