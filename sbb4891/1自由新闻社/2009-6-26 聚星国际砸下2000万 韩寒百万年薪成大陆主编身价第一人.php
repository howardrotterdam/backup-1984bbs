<br />
　　本报讯 据《新闻晨报》报道，从韩寒在博客上放风要自己当主编办杂志，到最高千字两千元的天价稿酬征集稿件，这本号称要“给当代有灵魂的青年看的”潮流杂志尽管尚未出炉，却已经赚足了眼球。如今眼看就要7月，却不见任何面市迹象。日前，该杂志投资方透露，由于主编韩寒正全力以赴在赛车场上，分身乏术的他不肯降低对杂志品质的追求，为此只好延期上市，目前大致定在8月，首印30万册。同时，他们还透露，主编韩寒薪酬高达7位数，是目前中国薪酬最高的杂志主编。<br />
