撰写《赵紫阳软禁中的谈话》　剖析好友晚年心境赵紫阳气功师　宗凤鸣逝世 <br />
<br />
中共总书记赵紫阳生前密友兼气功师、《赵紫阳软禁中的谈话》作者宗凤鸣，上周四（７日）凌晨一点半，因胃出血逝世，终年 89岁。根据宗老生前交代，不发讣告、不开追悼会，一切从简。宗家已于周六上午在北医三院举行告别仪式，遗体送昌平火化，前来告别宗老的亲戚、朋友和有关领导等达 100余人。<br />
<br />
宗凤鸣家人昨日接受本报电话采访时，对外界的关心表示感谢，但由于宗老生前已交代，家人生活须低调，因此婉拒媒体的采访。据悉，下周二（ 19日）将年届 90岁的宗老，他出殡当日，赵紫阳家人等出席了他的告别仪式。<br />
<br />
宗凤鸣身后为赵所送的题词：「人老精神不能衰，改革之志定不移」。<br />
<br />
赵紫阳（右）遭软禁期间，宗凤鸣多次前往赵家探望老领导。<br />
<br />
详细记述与赵近百次谈话 <br />
 <br />
赵紫阳逝世后，宗凤鸣（中）和毛泽东前秘书李锐（右二）等前往赵家悼念。左二为赵的女儿王雁南。<br />
<br />
流亡海外的异见人士严家祺对宗凤鸣的逝世表示哀悼。他称，宗老难能可贵的是敢于为赵紫阳伸张正义，并欣赏他对历史的忠诚，没有因为胡温身居高位，而在书中删除赵对胡温的评价。与赵紫阳同乡的宗凤鸣，因 2007年 2月出版著作《赵紫阳软禁中的谈话》而为人熟识，亦因此让世人首次全面了解因六四事件而被罢官，并遭软禁长达近 16年之久的赵紫阳的心路历程。其间在 1991年 7月到 2004年 10月间，宗凤鸣以气功师的名义去位于富强胡同 6号的赵家，前后达上百次之多，他在书中详细记述了赵 81篇近百次的谈话，也记述了晚年的赵在软禁中的心境、心态和生活。他成为唯一获当局准许定期探访赵的非亲属外人。据悉，自赵紫阳在 2005年初逝世后，中共有关部门曾多次派人找宗凤鸣，劝他不要出版他和赵的谈话纪录，直至 2007年初书稿已交到出版社付印，当局还派人来跟他谈了一次，问他可否推迟一点才出版，但宗老心意已决，再次婉拒了来者的游说。宗老曾说：「看到这本书面世，算是对老朋友紫阳和惨案中的死难者有个交代了！遗憾的是这本书不能在大陆出版，让更多人看到赵紫阳的最后历程！」 <br />
<br />
遭中共软禁家人饱受压力 <br />
<br />
宗凤鸣的《赵紫阳软禁中的谈话》为研究中国上世纪八十年代及六四事件、研究中共第二代、研究中共体制改革留下一份珍贵的史料。但他亦因此遭中共当局软禁，家人亦一度饱受压力。宗老更在 2007年 2月因心脏病发，被送院抢救治疗。本报记者<br />
