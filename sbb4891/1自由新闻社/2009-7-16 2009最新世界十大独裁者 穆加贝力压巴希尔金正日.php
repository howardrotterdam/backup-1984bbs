美国《华盛顿邮报》发行的周末副刊杂志《大观（PARADE）》21日报道说，北韩领导人金正日在“世界十大独裁者”中位居第三。<br />
<br />
《大观》每年都评选出“世界十大独裁者”，去年将朝鲜国防委员长金正日评为世界十大独裁者之首，但今年将其排名降至第三位。<br />
<br />
该杂志每年都以美国国务院和国际人权团体“人权观察”、Amnesty International、Reporters sans frontières等方面的资料和情报为基础，对践踏人权、居民在领导者统治下承受的痛苦程度、他们行使的绝对权力的程度等进行分析，评选并公布世界十大独裁者。<br />
<br />
在今年的评选中，津巴布韦总统罗伯特-穆加贝“力压”金正日排在首位，成为世界最腐朽的独裁者。<br />
<br />
去年排名第六的穆加贝自1987年执政以后肆意进行铁拳统治，甚至将经济推向崩溃的边缘，失业率高达85%，在严重的通货膨胀局面下，政府于今年1月甚至发行了面值500亿津元的钞票。另外，去年8月份以后霍乱猖獗，导致3800多人死亡。<br />
<br />
在世界十大独裁者中排名第二的是苏丹总统奥马尔-阿尔-巴希尔，他去年也排在第二位。巴希尔是主导达尔富尔大屠杀的罪魁祸首，国际刑事法院(ICC)最近对他发出了逮捕令。<br />
<br />
位居第四的是缅甸军政最高领导人丹瑞。去年5月袭击缅甸的飓风“纳尔吉斯”造成14万人死亡或失踪，受灾居民达200万人以上，但丹瑞却推迟救助团体进入灾区，受到国际社会的谴责。丹瑞去年排在第三位。<br />
<br />
其次是沙特阿拉伯国王阿卜杜拉，他去年排在第四位，今年下降到第五位。在沙特阿拉伯，女性未经男性保护者的允许不能就业、旅游、求学、结婚，甚至不给女性发放身份证，而且连刑法都不存在。<br />
