2. Omar al-Bashir<br />
Sudan<br />
<br />
Age: 65 <br />
In power since: 1989 <br />
Last year's rank: 2 <br />
<br />
This month, the International Criminal Court issued an arrest warrant for al-Bashir on charges of war crimes and crimes against humanity for his contributions to the tragedy in Darfur. The ICC charged him with “ murdering, exterminating, raping, torturing, and forcibly transferring large numbers of civilians, and pillaging their property.” Bashir’s military campaign has been accused of driving 2.7 million people from their homes since 2003. <br />
<br />
U.S. link: Both former President Bush and President Obama have spoken out strongly against Bashir’s actions. Although we’ve imposed trade sanctions against Sudan, the U.S. still supplies the country with substantial amounts of sorghum and other food products. The U.S. also purchases from Sudan millions of dollars worth of gum Arabic, used in soft drinks, candy, and shoe polish. In 2008, U.S. trade with Sudan actually increased to $148 million. <br />
<br />
3. Kim Jong-Il<br />
North Korea<br />
<br />
Age: 67 <br />
In power since: 1994 <br />
Last year's rank: 1 <br />
<br />
Kim is believed to have suffered a stroke and has been absent from important public events. But his regime continues to be one of the world’s most repressive. Hundreds of thousands of citizens, including children, are imprisoned in labor camps for such “crimes” as hoarding food and “ anti-socialist” activities. <br />
<br />
U.S. link: Relations with North Korea have been dominated by Kim’s development of nuclear weapons. Secretary of State Clinton recently expressed concern over a possible succession struggle and signs that North Korea is preparing to test a long-range missile, which is considered highly provocative to South Korea and Japan.<br />
<br />
