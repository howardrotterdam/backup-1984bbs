Iran<br />
<br />
Age: 69 <br />
In power since: 1989 <br />
Last year's rank: 7 <br />
<br />
Ayatollah Khamenei has increased arrests of nonviolent opponents of his regime, women's-rights activists, ethnic and religious minorities, and journalists. In 2008, Khamenei’s government was the only one in the world to execute juveniles. Iranian law considers boys eligible for the death penalty at age 15 and girls at age 9. <br />
<br />
U.S. link: Despite the war of words between leaders of the U.S. and Iran, and concern about the possibility of Iran developing nuclear weapons, trade between the two nations skyrocketed in 2008, highlighted by American sales to Iran of wheat, corn, and pharmaceutical products. <br />
<br />
