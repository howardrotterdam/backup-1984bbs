1. Robert Mugabe<br />
Zimbabwe<br />
<br />
Age: 85 <br />
In power since: 1980 <br />
Last year's rank: 6 <br />
<br />
Inflation in Zimbabwe is so bad that in January the government released a $50 billion note ― enough to buy two loaves of bread. The unemployment rate has risen to more than 85%. In 2008, Mugabe agreed to hold an election, but it became clear that he would accept the result only if he won. His supporters launched attacks on the opposition, killing 163 and torturing or beating 5000. He ultimately signed a power-sharing agreement with opposition leader Morgan Tsvangirai, but since then Mugabe has broken its terms and installed his own people at the head of every ministry. Meanwhile, health conditions have reached crisis levels. More than 3800 Zimbabweans have died from cholera since August. <br />
<br />
U.S. link: Although U.S. leaders have called for Mugabe’s resignation, imports from Zimbabwe (primarily nickel and ferrochromium, both used in stainless steel) rose in 2008. <br />
