Burma (Myanmar)<br />
<br />
Age: 76 <br />
In power since: 1992 <br />
Last year's rank: 3 <br />
<br />
Cyclone Nargis slammed into Burma on May 2, leaving some 140,000 dead or missing and more than 2 million homeless. But Than Shwe delayed access to stricken areas by aid groups while he held a rigged election to approve a new constitution. <br />
<br />
U.S. link: The U.S. has condemned the military regime for 21 years and called for the release from house arrest of Nobel Peace Prize-winner Aung San Suu Kyi. <br />
