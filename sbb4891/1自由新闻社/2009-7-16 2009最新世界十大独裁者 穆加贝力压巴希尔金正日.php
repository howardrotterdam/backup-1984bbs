China<br />
<br />
Age: 66 <br />
In power since: 2002 <br />
Last year's rank: 5 <br />
<br />
Hu and the Chinese Communist Party won the right to host the 2008 Olympics by promising to improve human rights. Instead, Hu tightened restrictions on freedom of speech and religion. Repression of Tibetans and other minorities has increased dramatically. Political and religious dissidents can be held in “ Re-education Through Labor” camps for up to four years without trial. <br />
<br />
U.S. link: In September, China passed Japan as America’s #1 foreign creditor. In 2008, we imported about $340 billion worth of goods from China.<br />
