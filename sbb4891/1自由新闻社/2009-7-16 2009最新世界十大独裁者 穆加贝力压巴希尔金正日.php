Despite King Abdullah’s talk of reform, Saudi women still need permission from male guardians to work, study, travel, marry, or even obtain an ID card. The government opened a women’s branch of its Human Rights Commission, but only men serve on its board. There is no Saudi penal code, and trial defendants often cannot question witnesses. <br />
<br />
U.S. link: Dependence on Saudi oil―the U.S. imported $50 billion worth in 2008― has caused our leaders to overlook the royal family’s abysmal human-rights record. <br />
