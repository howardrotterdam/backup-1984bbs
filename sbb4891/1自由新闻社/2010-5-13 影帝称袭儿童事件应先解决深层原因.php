													<h2>温家宝：校园血案频发说明中国社会矛盾日趋尖锐</h2>
						
						
																											<div id="postmessage_614093" class="t_msgfont">温家宝：校园血案频发说明中国社会矛盾日趋尖锐<br />
凤凰独家]2010年05月13日 20:05凤凰卫视<br />
<br />
中国国务院总理温家宝在接受凤凰卫视访问时表示，频频发生的杀童案，说明中国社会存在深层次矛盾，而且日趋尖锐化，政府正在努力发挥基层的协调作用，维护公民的生命安全和权益，让社会更为和谐。<br />
<br />
中国两个月内发生五起幼儿园、学校凶杀案，在国际上造成了恶劣影响。原因不仅仅是治安的问题。被问及深层次原因，以及政府如何管控时，国务院总理温家宝表示，社会矛盾的深化和存在不公平问题是导致事件发生的根源。他说政府需要发挥基层组织的协调作用，保证民众，不仅仅是儿童的生命权益。<br />
<br />
温家宝说：“除了采取强有力的治安措施之外，我们还要注意解决造成这些问题的一些深层次的原因，包括处理一些社会矛盾，化解纠纷，加强基层的调解作用，这些工作我们都在努力去做。”<br />
<br />
陕西汉中周三弑童案后，公安部，教育部紧急部署，加强学校保安防范。不过，对于凶手的行凶动机，目前都没有清晰结果。社会舆论认为，亡羊补牢固然需要，但是不能仅仅以凶手精神不正常为理由；另外，民办教育机构的安全也是存在很大纰漏。当天公安部部长孟建柱也表示要引导偏执者正确面对人生困难。<br />
<br />
在采取紧急的必要的治安措施之外，中国政府已经意识到，弑童案的频频发生是社会矛盾尖锐化的显现。温家宝表示，中国政府已经开始加强全社会的管控能力，而且这个规划已经在设想当中。<br />
<br />
凤凰卫视 秦枫、王昭诚 天津报导</div>

							
							
							
							
