													<h2>国美大选 陈黄双方疯狂拉票</h2>
						
						
																											<div id="postmessage_847056" class="t_msgfont">国美今日大选 陈黄双方疯狂拉票<br />
<br />
新京报9月28日报道&nbsp;&nbsp;持续两个月之久的国美控制权之争今天将见分晓。9月26日，黄光裕方再次发表公开信拉票，其提名的董事邹晓春昨天也在香港召开新闻发布会，表示对投票结果有很大信心。不过，国美方面昨天并没有对股东大会可能的结果作出表态。<br />
<br />
贝恩已投票支持陈晓<br />
<br />
据国美电器方面透露，贝恩资本上周已经完成股东大会投票，支持以陈晓为首的国美现任管理层和董事会增发授权。而大股东黄光裕夫妇已于23日完成了投票。<br />
<br />
截至目前，陈黄阵营的主角都已完成投票，但决定陈黄之争胜败的绝大多数机构投资者并没有公布投票结果，这也让今天下午的股东大会充满了悬念。<br />
<br />
今年5月国美电器周年股东大会上，由于部分机构股东缺席，股东投票率仅为60%，大股东一票即能达到一半的通过比例。陈晓在近日接受媒体采访时呼吁1400位有姓名登记在册的股东参与投票。<br />
<br />
根据公开信息和媒体报道资料，贝恩资本持股9.98%，陈晓本人1.25%，陈晓女儿陈叶0.42%，永乐旧部员工3.45%，投资机构F&amp;C持股0.05%。陈晓阵营的持股比例为15.15%。<br />
<br />
而黄光裕阵营包括，郑建明持股2%，欧阳雪初及湖南职业投资者持股为0.6%，加上黄光裕家族本身32.47%的投票，黄氏家族的支持票超过35％。<br />
<br />
机构投票成胜负关键<br />
<br />
机构投资者如何投票将是决定陈黄胜负的关键。根据香港中央结算系统持股记录显示，在22日国美股权登记完成后，国美电器的持股结构发生了微妙变化：汇丰银行、渣打银行、大摩香港证券、溢利证券及大福证券分别持有国美股权25.88%、11%、3.72%、7.33%和2.88%。<br />
<br />
国美方面曾宣称支持陈晓的摩根士丹利减持了至少4%的股份，目前持有的股份不足5％。而受黄光裕家族委托的券商溢利证券和大福证券的持股比例增加。<br />
<br />
“决战”前夕，黄光裕方面仍在努力拉票。26日晚间，黄光裕方发表题为《国美电器创始股东再致股东同仁公开函》的公开信，呼吁股东罢免陈晓；重申发展的五年规划，明确承诺开店数量和销售规模等。<br />
<br />
此外，黄光裕方面透露，已经与多位国美高管进行过友好沟通，高管们表示“忠于公司利益而非某一个人”，也就是说，此前所谓“与陈晓共进退”的说法是一种误解，已经“被扭转过来了”。<br />
<br />
相关<br />
<br />
邹晓春谈“战后”布局<br />
<br />
称若大股东动议被否决，将分拆国美<br />
<br />
据记者了解，国美董事局主席陈晓昨天下午已经抵达香港，黄光裕提名的董事邹晓春已在香港多日。<br />
<br />
邹晓春昨天下午两点在香港铜锣湾怡东酒店召开了新闻发布会，表示对投票结果有很大信心。据其透露，今天的股东大会将会在现场点票，然后直接公布结果，在投票会议上还会有神秘人物参加。<br />
<br />
若黄光裕输，国美门店将分拆<br />
<br />
有媒体提问如果黄光裕方提出的动议在股东大会上获得通过，是否会解雇现任的董事，邹晓春表示暂时还没有决定。不过如果大股东提出的五项动议均被否决，黄光裕的400多家非上市门店将与国美上市门店分拆，“这是最无奈的结果”。<br />
<br />
若陈晓输，邹晓春加入管理层<br />
<br />
如果大股东提出的动议得到通过，邹晓春表示，国美会由董事局继续带领，他本人将与黄燕虹以执行董事身份参与国美管理，股东可以通过股东大会提出意见。<br />
<br />
五年有信心达到千亿营业额<br />
<br />
针对陈晓的五年计划，黄光裕方面也曾给出黄方的五年计划。邹晓春昨天表示对大股东的五年计划非常乐观，他预料陈晓的五年计划会带来700亿-800亿元营业额，而大股东的计划有信心达到1000亿营业额。<br />
<br />
黄方称陈晓应离开国美<br />
<br />
昨天，是陈黄“决战”前的最后一天。据香港媒体昨日报道称，9月25日，贝恩资本董事总经理竺稼、国美电器董事局主席陈晓以及黄光裕的妹妹、鹏润投资董事长黄秀虹在深圳进行和谈。陈晓更愿以1年后主动离开国美换取多方重新合作，希望促成股东、公司双赢局面。但是三方并没有达成一致意见。黄秀虹拒绝陈晓、贝恩资本方面的意见，导致和谈破裂。<br />
<br />
对于这一报道，黄光裕方面昨天发布了《创始股东关于近期双方谈判的立场》，表示自今年上半年以来，一直积极与陈晓及董事会沟通，但现任主席拒绝其合理要求。国美创始股东认为目前取消股东大会是不负责任的。<br />
<br />
黄光裕方面表示，陈晓应确定在短期内离开国美，国美董事会在11人基础上更选两名董事；如公司章程可以增加人数，那么应该增加董事会推荐的独立董事而不是执行董事，这样的结构才有利于公司管制。<br />
<br />
“如果陈晓有诚意达成任何有法律效力的确定性的东西，而不是在拖延时间，大股东会积极考虑对公司有利的方案。在依然存在重大不确定性的情况下考虑取消股东大会是不负责任的。”<br />
