近日，一项针对高校大学生“性与生殖健康状况”的调查报告出炉。这项调查的结果折射出一个令人担忧的事实：在高校学生性观念解放、性行为增多的同时，他们对避孕知识的掌握远远不够。<br />
<br />
　　这项调查报告的牵头人为武汉同济医学院计划生育研究所所长、武汉同济生殖医学专科医院院长熊承良，此课题是国家“十一五”项目。该调查历时两年，开始时间为2007年9月，调查对象为北京、长春、成都、南京、南宁、武汉、重庆等7个城市的8万余高校学生，包括在校本科生、硕士及博士研究生。<br />
<br />
调查结果显示：60%左右的学生持性解放的观念，70%以上学生对婚前性行为及未婚同居等行为持宽容态度。比较而言，大学女生、医科学生以及每月可支配费用400元以下的学生性观念较为保守，性行为的发生率低于其他类别学生。<br />
<br />
　　调查显示，14.4%的未婚高校学生承认有过性行为，而在首次性行为中采取避孕措施的为47%，在性行为中每次都采取避孕措施的只有28.7%。<br />
<br />
　　性行为中没有采取避孕措施的前三位原因分别是：没有计划所以未备避孕药具；觉得偶尔性行为不会怀孕；觉得使用避孕措施影响情趣。<br />
　　因此，有过性行为的学生中，有25%以上的学生都经历过意外妊娠。<br />
