<br />
　　其次,主办方应当明白,任何提议都不能违背《联合国气候变化框架公约》的要求,不能违背“共同但有区别的责任”原则。这一事件看似是“程序”问题,但没有人会相信主办方一再试图提出“丹麦文件”,是由于对联合国谈判规则“外行”,或者出了“技术性错误”。<br />
<br />
　　第三,主办方应当明白,气候变化问题必须由全球所有的国家来决定,而不能由少部分国家来决定,联合国框架下的谈判才是解决气候问题的主渠道。正如苏伟所说:“各方在谈判中已经达成的一致意见是,哥本哈根大会成果唯一有效的法律基础,应当是联合国气候谈判两个工作组的工作成果。”<br />
<br />
　　全球数十亿人都在关注哥本哈根大会,主办方急切的心态或许可以理解。但如苏伟所说,这是一个由谈判各方所推动的谈判进程,不能试图通过“从天而降”的文件来推动谈判。<br />
<br />
　　需要指出的是,说是主办方的问题,其实所有发达国家都存在这样那样的问题。要让谈判取得进展,包括丹麦在内的发达国家必须认真倾听发展中国家心声,保证会议遵循“巴厘路线图”、《联合国气候变化框架公约》及其《京都议定书》的原则和框架,必须积极回应发展中国家提出的提高减排指标以及提供技术转让和资金的要求。只有这样,才是让哥本哈根留名青史的正确途径。除此之外,别无他途。<br />
