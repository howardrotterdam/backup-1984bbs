今天上午，浙江省江山市举办江山大讲堂第五期专题讲座，中国人民解放军军事科学院战争理论和战略研究部副部长（副军级）毛新宇博士作题为《毛泽东国防战略思想与攀越国防科技的新高峰》专题报告。<br />
<br />
　　毛新宇夫人、全国妇联执委、中国女摄影家协会副秘书长刘滨，衢州市委常委、宣传部长高启华，衢州市政协副主席刘炳炎，衢州市政协原副主席祝瑜英、毛洪才等，江山市领导陈锦标、陆洪涛、陈幸尔、徐朝金等（含退岗在职领导）及部门乡镇负责人共400多人聆听了讲座。<br />
<br />
　　江山市委书记傅根友主持讲座，并介绍了毛新宇在学术研究上的成果，对报告内容进行总结。下边是听众对毛新宇同志的部分提问：<br />
<br />
　　1、听众：革命战争时期，我军装备力量和资源都不如敌人，却一次次地以弱胜强，最重要的因素是什么？<br />
<br />
　　毛新宇：我觉得这个问题问得很有意义。简要地回答有两条：第一，就是毛主席为我们制定了英明的建军路线，把党支部建在队伍基层连队。第二，是人民战争的理论，团结广大的支援正义的人民。这场人民战争，帮助我们最终战胜敌人，建立新中国。<br />
<br />
　　2、听众：在当今世界，我国国防科技主要优势在哪些方面？未来发展方向如何定位？<br />
<br />
　　毛新宇：你的提问我很感兴趣，刚才我的报告讲了，中国国防的优势在于，我们先后研制了原子弹、氢弹，又装备了二炮部队，还有洲际弹道导弹的研制成功。在这些发展的基础上，我国国防力量就有了重大突破，特别是近几年来，我军已掌握了多项世界尖端技术，我国国防现代化的优势，将进一步增强。<br />
<br />
　　3、听众：您来江山已经有几天了，还参观了江山的一些企业、景点，印象如何？<br />
<br />
　　毛新宇：我对江山的印象是很美好的。江山啊，山清水秀，景色秀丽，而且江山的企业，不管是水泥还是门业、家具，都是很具有实力的。谢谢。<br />
<br />
　　另外加一句，我看你们传递上来的小纸条，有的听众对我评价还是挺高的。我觉得，我还是要不断地学习和努力。说实话，今天这场报告，用尽了我的全部智慧和力量。我来做这个报告，感到非常光荣。这个报告，我之所以很感兴趣，在很多场合做了，是因为国防建设十分重要。<br />
<br />
　　4、听众：毛首长，下次您还要来江山吗？<br />
<br />
　　毛新宇：下次我一定要来。10月份举行的毛氏文化旅游节，我肯定要来。<br />
<br />
　　5、听众：您最喜欢的毛主席诗词是哪一首？能为大家朗诵一下吗？<br />
<br />
　　毛新宇：其实，我最喜欢《沁园春雪》，但今天我还是给你们朗诵《七律人民解放军占领南京》吧。为什么要选这一首呢？因为非常符合历史和现实。60年前，1949年4月21日，毛泽东、朱德发布了进军的命令，发动了渡江战役。没有60年前的辉煌壮举，就没有统一富强的新中国。&quot;钟山风雨起苍黄，百万雄师过大江。虎踞龙盘今胜昔，天翻地覆慨而慷。宜将剩勇追穷寇，不可沽名学霸王。天若有情天亦老，人间正道是沧桑。 &quot;<br />
<br />
　　大家让我唱歌，那我就清唱一首自己创作的《爷爷曾在这里走过》吧。<br />
<br />
