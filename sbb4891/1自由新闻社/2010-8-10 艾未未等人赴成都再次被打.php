<br />
&quot;China's judicial system is totally corrupt and paralysed. Even with a case that people internationally know, they don't give a shit.&quot;<br />
<br />
Ai said he was now at the complaints office but that he had been refused a receipt for his report and feared they would simply drop it in the bin. He said: &quot;We have to make the effort, but we can't really win.<br />
<br />
&quot;We know we can't really get [satisfaction] but we still have to go through the system C if you don't do it, that's your own fault for giving up your rights.&quot;<br />
