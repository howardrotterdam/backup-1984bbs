新华网8月30日电 明星妙手偶得之手迹被人称善，是常有的事，但是，被人视为珍宝而用千金收买的却不多。可是，最近就有新闻报道说，央视《开心词典》主持人王小丫的一幅习作绘画，竟被人以4800万元的天价买走了，这真是令人咋舌的目瞪口呆。<br />
<br />
明星的墨宝被天价收购并非是王小丫首例，前有大导演张艺谋的奥运会签名册，就曾被成龙花100万元买下，后来成龙被网友讥讽为冤大头。今天，王小丫也创造了一个画价的奇迹。王小丫的画真的能一画值千金？恐怕信者寥寥，因为，我们知道王小丫精于主持，绘画只是她的业余爱好，就像她喜欢发烧音响一样。至于她的笔墨丹青，远没有达到独步天下的地步。我们看她的作品“水墨修竹”，无论是构图，还是技法，都谈不上有什么奇妙、神化的地方，不过是“胸有青竹”而已，不能认定为独出手眼，可笑的是，在拍卖现场，央视主持人白岩松竟把王小丫称作“大师”，不知以擅长画竹的郑板桥在泉下听到这句话会否喷壶一笑。<br />
<br />
我国从古至今，画家数不胜数，能够卖到天价的画也只限于古今的名家高手，如唐代的吴道之，明代的唐伯虎，现代的张大千等等名家，今天王小丫怎么可能忽有神来之笔，画竹成趣，其画价甚至直逼当今吴冠中的字画，而吴冠中的画在拍卖行也不过每平方尺200多万元，既然不是，那么必定有因，铜都颜小四认为，就是炒作，要么是媒体炒作，要么是自我炒作，要么就是买家炒作。<br />
<br />
当今，炒作之风犹烈，为一个“利”字，什么都可以炒，像王小丫的画买出了天价，肯定也是一个炒作，炒家无非是想制造新闻效应，或者是商业包装、造势，他们完全不考量作品价格与市场价格的统一，只是以明星身价定画价，随人就市，至于书画本身不过是个依附物，出高价买的是明星的人气，而那个收藏王小丫画的买家，明知其画有价无市而争之，只是借冕扬名罢了。<br />
<br />
时下，我们对明星祟拜热依然不减，甚至到了爱物及鸟的地步，凡是明星的东西都是物有所值，小到手套鞋帽，大到首饰房车，都能成为公众收藏的宝物，犹如孔子的衣冠，或是佛祖的舍利子，一律视为圣物。前年，美国流行音乐天王杰克逊生前戴过的一只水晶白手套，不是就被人以35万美元的天价拍下么。<br />
<br />
其实，某些明星的东西毫无收藏意义，因为做为物质，它与同类物质没有什么两样，“星星还是那颗星星，月亮还是那个月亮”，它不可能像太极一样，是生两仪，两仪生四象，给你鸡生蛋，蛋生鸡，至多是纪念价值，然而，所谓纪念价值，是精神性的东西，而不是物质性的东西，正如佛在心中，而不是佛在庙中，有些人，就是盲目地把明星的鹅掌当成龙爪，手迹当成墨宝，认为奇货可居，藏之深山，传之后世，殊不知，明星的东西，至多不过是暂时留有余香罢了，多年以后，它不会阿弥陀佛显灵。<br />
<br />
明星的东西，我们不是说不能收藏，而是要讲科学鉴别，理性收藏，否则，一旦明星遗臭于世，他们的藏品将变得分文不值。<br />
