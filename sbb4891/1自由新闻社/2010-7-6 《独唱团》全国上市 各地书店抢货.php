													<h2>《独唱团》全国上市 各地书店抢货</h2>
						
						
																											<div id="postmessage_723345" class="t_msgfont">2010-07-06 01:35:00　来源: 红网(长沙)　跟贴 22 条 手机看新闻<br />
<br />
<br />
核心提示：韩寒主编杂志《独唱团》今日将在全国上市。为防止串货，每本杂志背后都印有地区标码，可以视为当地的“限量版”。同济大学副教授王晓渔表示，不一定会去看，但是一定会去买，在经济上表示支持。从某种意义上说，这本杂志一出生就已风华正茂。<br />
<br />
<br />
<br />
<br />
<br />
<br />
 <br />
<br />
红网-潇湘晨报7月6日报道 对《独唱团》的关注源于它的主编韩寒，这个少年成名，并被视为80后代言人的“明星”的一举一动都能引人注目。同济大学副教授王晓渔毫不吝啬其溢美之辞――我不一定会去看，但是我一定会去买，在经济上表示支持。从某种意义上说，这本杂志一出生就已风华正茂。 <br />
<br />
《独唱团》今日将在全国上市，哪里能买到？这成为昨日最热门的话题。“我们发往湖南的货会在子夜到达当地的物流公司，根据协议，物流公司会在早上九点之前将货配送到我们指定客户的手中。”出版方天津华文天下图书有限公司媒体公关部主管刘琦昨日告诉潇湘晨报记者。 <br />
<br />
据了解，通过物流公司送货上门可以省去容易产生盗版的一些中间环节。为防止串货，每本杂志背后都印有地区标码，可以视为当地的“限量版”。 <br />
<br />
报刊亭询问的不少，但昨日尚未接到通知 <br />
<br />
老周是韶山南路一家报刊亭的老板。昨天下午，他一边摇着扇子，一边忙着为接踵而来的顾客拿出各种饮料、报刊。 <br />
<br />
“你说的是韩寒新出的书吧？我这还没有啊。”听明记者的来意，老周笑着说，“我一看你就知道是做记者的，这两天已经有四五个人来问这个事了。”说起《独唱团》，老周直言，目前他还没有接到邮局的通知，“我这是邮政系统的，长沙的报刊亭，七成属于邮政，但邮局还没通知我们，什么时候可以去拿《独唱团》。”不过，老周告诉记者，一旦接到通知，他会拿5本，“对于一本新杂志来说，这算很多了”。 <br />
<br />
从老周那出来，记者又找到一家报刊亭，不同的是，这家报刊亭属于民政系统。“《独唱团》？我还没听说过啊。”老板于先生疑惑地说。 <br />
<br />
新华书店：非常规方式确保12点前上柜 <br />
<br />
作为省内最大的图书连锁卖场，湖南新华书店对《独唱团》的上市做好充分的准备。 <br />
<br />
昨天傍晚，记者从新华书店获悉，该店预订的首批1000本《独唱团》已抵达货站，“《独唱团》将在明天9点全国统一上市，所以现在书都在货站了。”省新华书店集团图书音像发行公司经理胡彬介绍说，为了让读者能在第一时间买到《独唱团》，他们将采取非常规的方式，“保证在明天12点前上柜”。 <br />
<br />
据悉，首批1000本《独唱团》将在全省新华书店配送。“不过会首先保证湖南图书城和袁家岭新华书店的销售需求。”胡彬告诉潇湘晨报记者，之所以只预订1000本，是因为《独唱团》发行方在全国图书流向方面有自己的调配计划，“不是想要多少就拿多少，不过如果销势很好，我们也会考虑再次进货。” <br />
<br />
胡彬说，湖南图书城和袁家岭新华书店会在现场的电子显示屏内将《独唱团》列为推荐图书，此外还会做一些现场宣传活动，引导读者购书。 <br />
<br />
叶洋书社：长株潭的民营书店今日有货 <br />
<br />
“7月6日早上物流公司会把杂志送到我们这里，我们书店把杂志上架，并会把货当日发往各市州的民营书店。像长株潭这样距离比较近的地方，当天就可以拿到杂志。距离远一点的地方，比如常德，我们可能会在晚上发货，杂志后天到。”位于长沙定王台书市一楼59号的叶洋书社负责《独唱团》在湖南地区民营书店的销售，书社负责人叶国军昨日说。 <br />
<br />
“明天货一到，我们就会在定王台门市部上架，同时把货发给湖南省范围内的客户。”叶国军说，《独唱团》炒了很久了，早在去年8月他们就知道有这么一本杂志。在他看来，韩寒作为青年作家受到很多人的追捧，因此杂志到货了以后肯定有很多人买。目前主动找他们要货的人很多，“比如怀化下面一个县城书店就找到了我们，大家都想在同一个区域内占领先机嘛。” <br />
<br />
虽然订货情况不错，但是对于杂志到底是不是好卖，叶国军说，这些都取决于杂志本身的质量，取决于读者对它的反响。紧接着他加了一句：“应该，应该好卖。不管如何，大家先把货抢到再说。” <br />
<br />
