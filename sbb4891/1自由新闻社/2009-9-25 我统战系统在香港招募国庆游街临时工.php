													<h2>六七暴动</h2>
						
						
																											<div id="postmessage_263121" class="t_msgfont">维基百科，自由的百科全书<br />
<br />
跳转到： 导航, 搜索<br />
六七暴动，亦称六七左派工会暴动、香港五月风暴，当时参与者称之为反英抗暴，1967年5月6日于香港爆发。香港亲共的左派在中国大陆文化大革命的影响下，展开对抗英国殖民地政府（港英政府）的暴动。事件由最初的罢工、示威，发展至后期的暗杀，出现暗杀名单、放置炸弹。结果51人直接在暴动中死亡，另外超过800人受伤，死亡人数仅次于1956年双十暴动。六七暴动是香港发展的分水岭，令香港人对共产党政权失去信心，亦促使港英政府改善施政。<br />
<br />
前奏<br />
参见：天星小轮加价事件及一二・三事件 <br />
1966年4月，由于天星小轮加价五仙由五仙加至一毫即加价一倍，引发九龙区出现天星小轮加价事件，显示殖民地政府治下的香港社会内，充斥不安定的情绪。<br />
<br />
1966年5月16日后，文化大革命在中国大陆展开。中国国务院中，原本负责港澳事务的廖承志在8月指示下在香港发动文化大革命。9月至10月，各地红卫兵四出串联。<br />
<br />
1966年12月3日，澳门发生一二・三事件。一件警民冲突事件，引发澳门左派到澳督府示威，要求警方道歉下台。澳葡政府出动军警镇压，导致十多人死亡，百多人受伤。1967年1月初，澳门左派发动罢市，中国更断绝澳门食水供应。葡萄牙政府最后屈服，接受左派的要求，并一度提出即时交还澳门。香港的左派到澳门祝贺，同时学习澳门左派的斗争经验。月中，原本负责港澳事务的廖承志被红卫兵夺权。<br />
<br />
[编辑] 经过<br />
1967年5月，位于九龙新蒲岗大有街的新蒲岗塑胶花厂发生劳资纠纷。<br />
<br />
5月5日，工潮开始恶化。部分工人阻止工厂出货，场面紧张。<br />
<br />
5月6日，防暴警察到场戒备，但在厂外工人不但没有散去，还与在场警察发生冲突，21名工人被警方逮捕，还造成多名工人受伤，聆讯后更有8人被判入狱。工会代表前往警署亦被扣押。<br />
<br />
5月7日，工人、工联会代表与其他支持者上街集会示威。示威者仿照中国大陆文革的做法，手持《毛语录》，高喊中国共产党口号。警方施放催泪弹及木弹驱散示威者，拘捕127人。<br />
<br />
5月11日，新蒲岗塑胶花厂工潮演变成暴动，工人囤驻在新蒲岗街道，与警察对峙，又用石头和玻璃樽袭击警员。警察开枪镇压。当局鉴于事态严重，于当晚宣布东九龙实施宵禁，所有后备警员取消休假候命，事件中有百多人被捕，并造成两人受伤。<br />
<br />
5月13日，九龙新蒲岗暴动蔓延至黄大仙东头徙置区和土瓜湾，大批群众在街上聚集，暴徒放火烧车及黄大仙徙置区职员宿舍，又攻入新区办事处和学校捣乱。当局出动大批英军和防暴队镇压，又将宵禁时间提前至傍晚6时开始。之后北京报纸称“港英政府行为是民族迫害，镇压群众是野蛮的法西斯暴行”，支持“香港市民上街抗暴”。<br />
<br />
5月15日，北京外交部向英国代办提出抗议，并发动北京群众在英国驻华代办门外示威。香港左派报章报道北京支持的消息，由左派学校学生在街头向路人派发；中国银行亦在中环总行的屋顶装上扬声器，进行革命宣传。<br />
<br />
5月16日，香港的亲共左派宣布成立港九各界同胞反对港英迫害斗争委员会，简称斗委会，由工联会理事长杨光出任主任。斗委会随即以“反英抗暴”为口号，联合各亲共团体数百人，手持《毛主席语录》前往港督府（今礼宾府）示威游行，并在港督府门外张贴大字报。港督府一度成为集体抗议示威的主要目标。港府重申要维持法律秩序，限制到港督府请愿人数，抗议行动于是转到中环花园道和皇后像广场一带。港九各地陆续有集会和示威，参加者除工人外，亦有学生及其他群众，期间拱北行政府新闻处旧址（今长江集团大楼）播放欧西流行曲去镇压示威者。<br />
<br />
5月21日，暴动蔓延至中环并进一步恶化，警方发射催泪弹镇压。<br />
<br />
5月22日，中区的骚乱更加严重，示威者与警察发生流血冲突，167人被捕。暴动的工人及学生以左派报馆、银行、国货公司、学校等为据点，出动示威，袭击警察及仍然行驶的公共交通。防暴警察到场以催泪弹、警棍等驱散后，示威群众即迅速撤回据点，令警察疲于奔命，当局最后要在港岛北岸实施宵禁，是香港岛在战后首次宵禁。<br />
<br />
5月23日起，巴士、电车、煤气公司、天星小轮等亦开始出现定时罢工。<br />
<br />
6月3日及10日，《人民日报》刊登支持香港左派反英抗暴的社论。<br />
<br />
6月24日，左派工会发动联合大罢工。约60,000名工人响应。政府及公共交通工具公司强硬对付罢工，将旷工雇员即时解雇。<br />
<br />
6月27日，左派学校罢课一天。<br />
<br />
6月29日，左派发动联合大罢市，副食品价格一度上涨。<br />
<br />
7月2日，罢市停止。<br />
<br />
7月8日，约百名大陆民兵在沙头角与香港的警察枪战，冲突造成五名香港警察死亡。香港出现传言，指北京打算收回香港。<br />
<br />
7月10日，周恩来传达毛泽东指示对香港“不动武”。<br />
<br />
7月初以后，左派的暴力行动进一步升级，开始以罐头罐制造土制炸弹及珠江汽水的汽水瓶来制造燃烧弹袭击警署，并以镪水（盐酸和硝酸的混合物）从高处袭击经过的警车及公共交通，左派学校的实验室成为了炸弹的制作工场。港府则对左派还击，首次出动英军搜查左派工会。之后陆续派军警搜查各左派据点，使用紧急法例的权力，拘捕及关押部份左派份子头目，将他们拘押在摩星岭白屋的政治部囚室，部分人之后被逮解出境。<br />
<br />
7月12日，港英政府同时于港九两地同时实施宵禁，是暴乱爆发两个多月来的第一次。<br />
<br />
8月4日，政府出动访港英军航空母舰上的直升机从天台降落，上下夹攻左派在北角的据点、位于英皇道与糖水道交界、大型国货公司华丰国货所在的侨冠大厦。警方在大厦内除搜出各式炸弹武器外，还发现有设备完善、包括有手术室的战地医院。<br />
<br />
8月9日，根据紧急法令，警方封闭多间左派学校及发出煽动言论的左派报社，包括《香港夜报》、《新午报》、《田丰日报》。<br />
<br />
左派则以升级炸弹行动还击，在港九各处闹市放置真假炸弹。炸弹上多数以大字写有“同胞勿近”，电车、巴士、街道上都有发现。炸弹袭击除了扰乱市民生活外，市民人心惶惶，更造成无辜死伤。一名7岁女童及其2岁弟弟即在北角被一包装成礼物的土制炸弹炸死。但当中亦有发现假炸弹。而负责拆除炸弹的警方及英军拆弹专家亦有被炸死或炸伤。<br />
<br />
8月22日，英国驻北京代办处被红卫兵放火烧毁。周恩来次日向英国政府道歉。<br />
<br />
香港市民对暴力行为普遍出现反感。很多不受左派控制的传媒都反对暴动论，支持政府及警察维持秩序和治安。当中商业电台节目主持人林彬多次在节目内对斗委会的目标和行为加以讽刺及贬斥；并以《欲罢不能》为节目名称，暗讽左派的罢工号召未能成功瘫痪香港。<br />
<br />
8月24日，林彬于上班途中被人伏击。伪装成修路工人的凶徒将林驾驶的汽车截停后，放火将林及其堂弟烧至重伤，二人其后死于医院。《明报》社长查良镛亦因为在报章内明确反对文革及暴动，收到死亡恐吓而一度离港暂避。<br />
<br />
真假炸弹继续在闹市伤人，直至10月之后方才稍为平息。10月底，政府为提高市民信心及制造太平气氛，推出本土的香港节，抗衡左派的“十一国庆”及右派的“双十国庆”，左派则以数百大小真假炸弹阻止市民参加。12月中，周恩来向香港的左派下达直接命令停止炸弹风潮，六七暴动亦告终结。<br />
<br />
[编辑] 影响<br />
据统计，六七暴动造成最少51人死亡，当中包括11名警察，一名英军拆弹专家及一名消防员。受伤人数超过800人，包括200名警察。暴动其间亦造成经济损失，部分市民变卖财产离开香港，造成香港的第一轮移民潮，市区楼房价格因此而急剧下滑。[1][2][3]<br />
<br />
暴动平息后，香港的亲共左派组织受到一定程度的破坏，不少地下组织在事件中曝光，有部分人被遣返大陆。左派暴力抗争的手法失去民心；暴动后，有不少之前属于或是接近左派的人士，从此脱离左派组织。很多香港市民亦对左派及中国共产党亦怀有戒心，对他们“避之则吉”。直到九七之后。当主权转移后特首董建华宣布颁发大紫荆勋章于当时左派负责策划暴动的头号人物杨光之时，引起全港社会各界哗然，认为是对当年左派暴行的一种肯定。<br />
<br />
香港殖民地政府研究六六、六七两次暴动成因后，察觉到有需要适量调整过时的管治策略。政府透过主动关心及改善一般市民民生，减少社会不公平现象，同时缓和市民不安情绪及与政府的对立。政府在暴动结束后一年，即1968年已着手修改劳工法例，大幅加强对劳工的保障。为改善与市民在地区层面上的沟通，进行首次地方行政改革，包括于1968年成立民政处（即今日的民政事务总署）。暴动亦间接催生了香港在七十年代在教育、医疗、廉政、房屋等各方面的改革。<br />
<br />
同时政府发现青少年空闲时间缺乏可供参与的活动而被左派招揽。于是决定大量增加文娱康乐活动。暴动后的1969年12月开始举办的香港节，就是当中的一个重要节目，藉以加强青年对香港的向心力。此外，渔农处（现渔农自然护理署）也在1968年7月开设林务营以解决青少年问题。<br />
<br />
香港警队在暴动中的忠诚及勇敢表现备受赞赏，1969年获英女皇赐予“皇家”封号，此衔一直保留至1997年6月30日主权转移。<br />
<br />
另一方面，由于六七暴动期间，不少公共巴士路线被逼暂停服务（部份更没有重投服务），引致不少九座位载客白牌车应运而生，获准到市区载客，最终令到殖民地政府认为白牌车可以辅助公共交通不足，促成1969年将白牌车合法化，成为今日的公共小巴，座位限制由9座位提升至14座位（1988年增至16座位），对香港公共交通的发展有深远影响。<br />
