													<h2>相声的派系与郭德纲的失势</h2>
						
						
																											<div id="postmessage_782443" class="t_msgfont">没有人怀疑侯跃文是个相声演员，可他在央视春节联欢晚会上最著名的节目是与赵丽蓉合作的《英雄母亲的一天》以及与黄宏合作的《打扑克》，两个小品！你还记得侯跃文在春节晚会上说过什么相声节目吗？这与郭德纲被反三俗又有什么关系？<br />
<br />
当年轻人追随娱乐记者的教导，把眼球聚焦在英皇向华强的神话战争时，被冠上太多德艺双馨名号的戏曲曲艺界的内斗早就无人关心了。因此，即使郭德纲面临反三俗，也之被认为只 是官方与民间的一次较量――时间会埋葬一切，即使亚历山大继承人间的世界大战，也早已湮没在流沙之下，何况几个说相声呢。但事情，其实并没有看上去那么简单。<br />
<br />
近三十年的相声演员帮派，有三个自成体系的集团。北京的马季――姜昆体系，侯宝林侯跃文体系和马三立马志明的天津系统。一方面，这个派系分割既源自相声早期的师承关系，也同样与49年后骑马入城那个帝国的政争密不可分。<br />
<br />
马季是侯宝林的得意弟子――但帝国的文革改变了这一切。在文革后，马季自成体系，培养出了姜昆、冯巩等几个新一代的力量，占据了春节晚会的舞台。在相声的第三次高潮――用电视推广向全国这一过程中，马季势力做到了最大，一直到今天，马季姜昆体系与官方和媒体的关系都是最密切的，姜昆是曲协的主席，冯巩在青联的位子不论，冯对于几个曲艺学校的控制也是相当严密的。<br />
<br />
侯宝林派系稍有不同。其实侯宝林在文革前很得新政府欢心――尤其与刘A少奇家有一些私交――当然，在文革后，一切都变了。侯宝林的早逝，让马季最终无所顾忌，只剩下“丹朱”一样的侯跃文，除此之外，侯宝林的关门弟子师胜杰也是侯家势力的悍将，但师胜杰的根据地主要在东北，与北京没有太多交际。不过，烂船尚有三斤钉，侯跃文依然保有铁路文工团一片天地，自成独立王国，与马季姜昆系统保持微妙的关系，也有讨价还价的资本――比如，春晚是马姜系的禁脔，于是侯跃文照样能够上春晚，但他在春晚最著名的节目，却是小品，比如捧红赵丽蓉的《英雄母亲的一天》，与黄宏合作的《打扑克》。<br />
<br />
北京的相声以与官方密切而特色，天津则仍旧保佑原先的市井风格的相声――或者说，原教旨主义的相声。马三立与他的儿子马志明，并不在意北京的风光，因此在全国范围内，他们的名声、出镜率也远远不如处在政治文化中心――也就是媒体中心的北京那些马姜侯家那么高。小圈子里的小圈子，是天津相声作为派系的风格，而原教旨，也就是对传统相声的重视，更是天津相声的特点――这个特点，在郭德纲对传统相声的强调中也能看出明显的天津烙印。<br />
<br />
当然，相声的派系也不仅仅只有这三个，北京的还有北京土著帮，天津帮内部也是山头林立，北京天津外也有还势力，不论，这三个派系基本是最主要的。而且，这三个派系相互并非井水不犯河水，自知衰弱的不可避免，侯跃文承侯宝林“遗训”，拜到早已亡故的天津赵佩茹门下，与天津的李伯祥、高英培等同门，是主动拉近侯体系与天津体系的距离。<br />
<br />
郭德纲的出场也就是在这个大背景下。郭德纲在天津学艺，理所当然属于天津的那些是是非非，但进入北京后，郭德纲需要有个保护人，这时，他就顺理成章的投入到了与天津帮交好的侯跃文的门下――这当然会让天津帮多少有些小小的不满，但天津帮的不满并不是郭德纲的威胁，因为郭德纲的目标是北京和全国的市场，并没有太关注天津的小圈子。真正不满的是马姜的正牌官方体系。<br />
<br />
从辈份上来说，郭德纲拜在侯跃文门下，那就是侯宝林的第二代――姜昆从马季这里算，其实也是。但从时间上讲，郭德纲搞茶馆相声是96年，在那个时候，姜昆等人早就不说相声了――或者说，早就不说逗乐的相声了。不仅他们不说，他们的徒子徒孙也都被阉割干净了。现在郭德纲在2000年以后的逐渐成名，不仅侵占了他们的市场，更让他们在业内成为笑柄――当然，也是郭德纲自己相声的笑柄。<br />
<br />
不仅如此，郭德纲还搜罗了一批早的文字辈演员，也就是李文山张文顺王文林等，这些姜昆的前辈，马季的同辈，在马姜体系下没有分一杯羹，因此也了一跟着郭德纲赚钱。这就更犯了姜昆的忌讳。<br />
<br />
最后，才是郭德纲所谓的低俗影响了“曲协掌门”姜昆，毕竟，在《着急》之后， 姜昆早忘记什么是《左派行医》了。<br />
<br />
郭德纲这几年官司不断，主要是马姜体系的打压，而郭德纲之所以不倒，也是因为侯跃文在的关系。然后，大家都知道，侯跃文死了，郭德纲掺和进了侯跃文女儿与耀华的官司中，侯家这个靠山，是再也没有了。<br />
<br />
于是，在只晓得卓娅舒拉某人的最高指示下，在马季姜昆体系及其媒体扈从的声讨下，郭德纲成了三俗的靶子。</div>

							
							
							
							
