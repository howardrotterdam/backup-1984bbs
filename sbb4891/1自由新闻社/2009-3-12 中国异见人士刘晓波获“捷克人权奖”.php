																											<div id="postmessage_51208" class="t_msgfont">刘晓波的受难与获奖<br />
<br />
03月 12, 2009 at 8:58 am ・ Filed under 贡献常识&nbsp;&nbsp;冉云飞<br />
<br />
<br />
没有人愿意受难，坚毅如刘晓波也不例外；没有人愿意进监狱，勇敢如刘晓波亦然。但刘晓波为了自己和同胞的尊严与幸福，依然做出在聪明人一看必进监狱的温和举措。六三六五二十年来，他身处艰危之际，在日常生活都受到诸多监视、不自由的环境之中，做出许多艰辛的努力，写了大量抨击专制独裁的启蒙文章，使不少禁锢在黑屋子里生活的人，找到一点光亮，呼吸到一点新鲜空气，不致于因灰心而颓唐，不致因伤心而疯狂，不致因绝望而自废武功。<br />
<br />
<br />
晓波二十来年他自己也有一个转变过程，早期的战斗性檄文较多，颇有攻击力与火气，鼓掌欢迎的人也不算少。他由黑马到被诬为“黑手”的过程，我们当然是知晓而且敬佩的。但我更看重，他现在越来越理性的努力，在民主宪政大框架下不懈的追求。我不是要否定晓波早年的努力，他早年的努力自有其光辉不灭的作用。没有早年他做的一切努力，也就没有他今天这样的转型。两相比较，我更能认同如今他所做出的努力，这些努力对多灾多难的中国更能够带来持久绵延的影响。这些影响也许一时半会儿看不到它结出太过醒目的硕果，但对我们追求民主自由的步伐，却有相当良好的矫正作用。二十多年前，我就是他的读者；十多年前，我有幸认识他，一直为他许多不懈努力击节。我们所做的努力要比他差了不少，而他的努力是引领我们这个时代不可替代的精神资源。他坚忍不拔、为自由民主鼓与呼的精神资源，其辐射作用逐渐扩张开来，激励着我们和他一同努力，让追求民主自由成为这个时代中国的最强音。<br />
<br />
 <br />
<br />
一个良好的社会当然希望政府与民众能够平等地对话，即便还没有平等的对话，至少也不应该因自己的恐惧而使对方的恐惧增加，我们希望政府能清醒地意识到这一点。我们作为公民，固然手无缚鸡之力，身无长物，有的只是一颗追求自由的心和持续不断的努力，但似乎让执政者恐惧，其实是大可不必的。要结束双方的恐惧，结束我们目前这个不自由不民主的制度，对其进行渐进的宪政转型才是当务之急。事实上，在专制制度下受捆绑的不只是我们普通民众，官员等有权势的人也得不到真正的幸福，走在哪里都看上去耀武扬威，前呼后拥，风光无比，只有他们自己才知道一个人想偷偷地去吃碗小面，静静地享受一下自己的生活，几乎是不可能的。将秦始皇弄到历史上第一暴君的李斯够牛逼，到头来连领着自己的儿子，带着自己的小狗去追逐野兔的普通要求都被剥夺了。历史与现实清楚地证明，专制制度是一种扼杀他人而且自杀的、咬人且自噬的人类最邪恶的制度。晓波为破解专制，推广自由宪政所做的诸种努力，不仅是一种良好的自救，也是对敌对者的真正救赎。<br />
<br />
 <br />
<br />
今天捷克“people in need”基金请捷克著名的民主自由争取者哈维尔亲自将一个人权Homo Homini奖颁予刘晓波及零八宪章的签署者，这是一份道义上的激励和动人的友情声援。晓波暂时受监禁的命运和零八宪章签署者目前受到诸多干扰的客观现实，不可因一个奖项颁布而有所改善。但我们有理由相信，任何对善举的声援，对正义的伸张与支持，必将为社会的改变做出相应的成就。在此，我们再次呼吁当局善待刘晓波并尽快释放他，以释放出更多的善意和实行政革的决心，以便大家在经济危机之下实现有做人底线的吴越同舟。<br />
