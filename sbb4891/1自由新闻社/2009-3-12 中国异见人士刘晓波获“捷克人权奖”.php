<br />
附颁奖词：<br />
<br />
每年，我们（People in Need）都会将Homo Homini奖颁给那些为人权、民主事业和非暴力解决政治冲突做出重要贡献的人士。<br />
<br />
08年度的Homo Homini奖颁给刘晓波先生，一位系狱的中国知识分子、政治抗议者、《零八宪章》首批签署者之一（其他签署者与刘晓波先生一同获奖）。<br />
<br />
我们将Homo Homini人权奖颁发给刘晓波先生，是对他30年来为促进中国的人权状况而作的富有洞见力的工作，以及他以坚韧和勇气为表达自由所做的抗争的一种承认。刘晓波是组织发起《零八宪章》的几百名知识分子中的一员，也是逐步成长的中国民主运动的领袖式人物。在中国政府持续的政治迫害下，争取民主的活动一直以来在坚持对话和非暴力原则下运行着。<br />
<br />
尽管刘晓波几十年来数次入狱，并长期生活在秘密警察的严格监控之中，他从未远离他的那些受到不公正待遇和迫害的战友们。刘晓波先生一直以来坚持自由地言说与写作，表达他对人权、非暴力解决冲突等普世价值的忠诚信仰。不仅如此，刘晓波先生在呼吁城乡居民的平等对待、更加彻底的环境保护等问题上的努力也应得到特别的赞许。<br />
<br />
“在我国总理和部长们与他们的欧盟同事开会磋商之际，在中国政府计划于布拉格举行欧中峰会的时候，我们强烈呼吁捷克政府关注并提出释放刘晓波的问题，”西蒙潘内克说道，潘内克先生是“People in Need”基金会创始人兼执行长官，捷克天鹅绒革命时期的学生领袖。“我们非常高兴和荣幸欧中峰会能够计划于捷克共和国举行，我们希望这座美丽的城市能够因其独特的历史轨迹带给欧盟和中国（一个伟大的国家）的政治领导人灵感，找到应对这场金融和经济危机的办法，惠泽全球。同时，在一个我们纪念我国和东欧诸地区实现向自由和民主和平转型20周年的日子里，我们无法忘记那些仅仅是公开表达自己观点、以和平方式寻求与政府对话的中国政治抗议者的现实处境和命运。如果欧中峰会能够在布拉格――这座正在纪念天鹅绒革命20周年的城池――举行，峰会将会在一个奇妙的环境下进行，如果会议能够以释放刘晓波这个议题开始，它将取得更加富有建设性的成果。”潘内克先生结论道。<br />
<br />
