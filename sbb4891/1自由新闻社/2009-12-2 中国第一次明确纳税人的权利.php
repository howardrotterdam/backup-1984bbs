<br />
　　依照宪法、税收法律和行政法规的规定，您在纳税过程中负有以下义务：<br />
<br />
　　一、依法进行税务登记的义务<br />
<br />
　　您应当自领取营业执照之日起30日内，持有关证件，向我们申报办理税务登记。税务登记主要包括领取营业执照后的设立登记、税务登记内容发生变化后的变更登记、依法申请停业、复业登记、依法终止纳税义务的注销登记等。<br />
<br />
　　在各类税务登记管理中，您应该根据我们的规定分别提交相关资料，及时办理。同时，您应当按照我们的规定使用税务登记证件。税务登记证件不得转借、涂改、损毁、买卖或者伪造。<br />
<br />
　　二、依法设置账簿、保管账簿和有关资料以及依法开具、使用、取得和保管发票的义务<br />
<br />
　　您应当按照有关法律、行政法规和国务院财政、税务主管部门的规定设置账簿，根据合法、有效凭证记账，进行核算；从事生产、经营的，必须按照国务院财政、税务主管部门规定的保管期限保管账簿、记账凭证、完税凭证及其他有关资料；账簿、记账凭证、完税凭证及其他有关资料不得伪造、变造或者擅自损毁。<br />
<br />
　　此外，您在购销商品、提供或者接受经营服务以及从事其他经营活动中，应当依法开具、使用、取得和保管发票。<br />
<br />
　　三、财务会计制度和会计核算软件备案的义务<br />
<br />
　　您的财务、会计制度或者财务、会计处理办法和会计核算软件，应当报送我们备案。您的财务、会计制度或者财务、会计处理办法与国务院或者国务院财政、税务主管部门有关税收的规定抵触的，应依照国务院或者国务院财政、税务主管部门有关税收的规定计算应纳税款、代扣代缴和代收代缴税款。<br />
<br />
　　四、按照规定安装、使用税控装置的义务<br />
<br />
　　国家根据税收征收管理的需要，积极推广使用税控装置。您应当按照规定安装、使用税控装置，不得损毁或者擅自改动税控装置。如您未按规定安装、使用税控装置，或者损毁或者擅自改动税控装置的，我们将责令您限期改正，并可根据情节轻重处以规定数额内的罚款。<br />
<br />
　　五、按时、如实申报的义务<br />
<br />
　　您必须依照法律、行政法规规定或者我们依照法律、行政法规的规定确定的申报期限、申报内容如实办理纳税申报，报送纳税申报表、财务会计报表以及我们根据实际需要要求您报送的其他纳税资料。<br />
<br />
　　作为扣缴义务人，您必须依照法律、行政法规规定或者我们依照法律、行政法规的规定确定的申报期限、申报内容如实报送代扣代缴、代收代缴税款报告表以及我们根据实际需要要求您报送的其他有关资料。<br />
<br />
　　您即使在纳税期内没有应纳税款，也应当按照规定办理纳税申报。享受减税、免税待遇的，在减税、免税期间应当按照规定办理纳税申报。<br />
<br />
　　六、按时缴纳税款的义务<br />
<br />
　　您应当按照法律、行政法规规定或者我们依照法律、行政法规的规定确定的期限，缴纳或者解缴税款。<br />
<br />
　　未按照规定期限缴纳税款或者未按照规定期限解缴税款的，我们除责令限期缴纳外，从滞纳税款之日起，按日加收滞纳税款万分之五的滞纳金。<br />
<br />
　　七、代扣、代收税款的义务<br />
<br />
　　如您按照法律、行政法规规定负有代扣代缴、代收代缴税款义务，必须依照法律、行政法规的规定履行代扣、代收税款的义务。您依法履行代扣、代收税款义务时，纳税人不得拒绝。纳税人拒绝的，您应当及时报告我们处理。<br />
<br />
　　八、接受依法检查的义务<br />
<br />
　　您有接受我们依法进行税务检查的义务，应主动配合我们按法定程序进行的税务检查，如实地向我们反映自己的生产经营情况和执行财务制度的情况，并按有关规定提供报表和资料，不得隐瞒和弄虚作假，不能阻挠、刁难我们的检查和监督。<br />
<br />
　　九、及时提供信息的义务<br />
<br />
　　您除通过税务登记和纳税申报向我们提供与纳税有关的信息外，还应及时提供其他信息。如您有歇业、经营情况变化、遭受各种灾害等特殊情况的，应及时向我们说明，以便我们依法妥善处理。<br />
<br />
　　十、报告其他涉税信息的义务<br />
<br />
　　为了保障国家税收能够及时、足额征收入库，税收法律还规定了您有义务向我们报告如下涉税信息：<br />
<br />
　　1.您有义务就您与关联企业之间的业务往来，向当地税务机关提供有关的价格、费用标准等资料。<br />
<br />
　　您有欠税情形而以财产设定抵押、质押的，应当向抵押权人、质权人说明您的欠税情况。<br />
<br />
　　2.企业合并、分立的报告义务。您有合并、分立情形的，应当向我们报告，并依法缴清税款。合并时未缴清税款的，应当由合并后的纳税人继续履行未履行的纳税义务；分立时未缴清税款的，分立后的纳税人对未履行的纳税义务应当承担连带责任。<br />
<br />
　　3.报告全部账号的义务。如您从事生产、经营，应当按照国家有关规定，持税务登记证件，在银行或者其他金融机构开立基本存款账户和其他存款账户，并自开立基本存款账户或者其他存款账户之日起15日内，向您的主管税务机关书面报告全部账号；发生变化的，应当自变化之日起15日内，向您的主管税务机关书面报告。<br />
<br />
　　4.处分大额财产报告的义务。如您的欠缴税款数额在5万元以上，您在处分不动产或者大额资产之前，应当向我们报告。<br />
<br />
