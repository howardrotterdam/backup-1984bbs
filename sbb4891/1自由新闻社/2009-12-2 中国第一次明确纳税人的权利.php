																											<div id="postmessage_352658" class="t_msgfont">中国发文明确纳税人权利 知情权居首<br />
<br />
<br />
新华网北京12月2日报道 记者2日从国家税务总局获悉,税务总局近日发布公告,首次对纳税人的权利和义务进行告知。 <br />
<br />
公告称,纳税人在履行纳税义务过程中,依法享有知情权、保密权、税收监督权、纳税申报方式选择权、申请延期申报权、申请延期缴纳税款权、申请退还多缴税款权、依法享受税收优惠权、委托税务代理权、陈述与申辩权、对未出示税务检查证和税务检查通知书的拒绝检查权、税收法律救济权、依法要求听证的权利、索取有关税收凭证的权利。 <br />
<br />
<br />
税务总局相关负责人指出,税务部门目前正在将提高纳税服务作为工作的重要任务,此公告的发布便于纳税人全面了解纳税过程中所享受的权利和应尽的义务,帮助纳税人及时、准确地完成纳税事宜,促进纳税人与税务主管部门在征纳过程中的合作,有利于营造和谐的纳税环境。 <br />
<br />
记者同时获悉,财政部、国家税务总局近日联合下发通知,对房产税城镇土地使用税有关问题进行明确,通知自2009年12月1日起执行。 <br />
<br />
通知称,无租使用其他单位房产的应税单位和个人,依照房产余值代缴纳房产税;产权出典的房产,由承典人依照房产余值缴纳房产税;融资租赁的房产,由承租人自融资租赁合同约定开始日的次月起依照房产余值缴纳房产税。通知还称,对在城镇土地使用税征税范围内单独建造的地下建筑用地,按规定征收城镇土地使用税。<br />
