 <br />
<br />
[提要] 国家税务总局近日在官方网站上发布关于纳税人权利与义务的公告，纳税人享有知情权、申请延期申报权、保密权、税收监督权、纳税申报方式选择权、申请延期缴纳税款权、依法享受税收优惠权等多项权利，同时有按时缴纳税款等多项义务…[你知道纳税人的权利义务吗]<br />
&nbsp;&nbsp;<br />
 <br />
 <br />
<br />
<br />
　　国家税务总局11月30日在官方网站上发布关于纳税人权利与义务的公告，就纳税人在履行纳税义务中的权利与在纳税过程中应负有的义务进行了详细解读。以下为公告全文：<br />
<br />
　　国家税务总局关于纳税人权利与义务的公告<br />
<br />
　　2009年第1号<br />
<br />
　　为便于您全面了解纳税过程中所享有的权利和应尽的义务，帮助您及时、准确地完成纳税事宜，促进您与我们在税收征纳过程中的合作（“您”指纳税人或扣缴义务人，“我们”指税务机关或税务人员。下同），根据《中华人民共和国税收征收管理法》及其实施细则和相关税收法律、行政法规的规定，现就您的权利和义务告知如下：<br />
<br />
 <br />
<br />
 <br />
<br />
　　您的权利<br />
<br />
　　您在履行纳税义务过程中，依法享有下列权利：<br />
<br />
　　一、知情权<br />
<br />
　　您有权向我们了解国家税收法律、行政法规的规定以及与纳税程序有关的情况，包括：现行税收法律、行政法规和税收政策规定；办理税收事项的时间、方式、步骤以及需要提交的资料；应纳税额核定及其他税务行政处理决定的法律依据、事实依据和计算方法；与我们在纳税、处罚和采取强制执行措施时发生争议或纠纷时，您可以采取的法律救济途径及需要满足的条件。<br />
<br />
　　二、保密权<br />
<br />
　　您有权要求我们为您的情况保密。我们将依法为您的商业秘密和个人隐私保密，主要包括您的技术信息、经营信息和您、主要投资人以及经营者不愿公开的个人事项。上述事项，如无法律、行政法规明确规定或者您的许可，我们将不会对外部门、社会公众和其他个人提供。但根据法律规定，税收违法行为信息不属于保密范围。<br />
<br />
　　三、税收监督权<br />
<br />
　　您对我们违反税收法律、行政法规的行为，如税务人员索贿受贿、徇私舞弊、玩忽职守，不征或者少征应征税款，滥用职权多征税款或者故意刁难等，可以进行检举和控告。同时，您对其他纳税人的税收违法行为也有权进行检举。<br />
<br />
　　四、纳税申报方式选择权<br />
<br />
　　您可以直接到办税服务厅办理纳税申报或者报送代扣代缴、代收代缴税款报告表，也可以按照规定采取邮寄、数据电文或者其他方式办理上述申报、报送事项。但采取邮寄或数据电文方式办理上述申报、报送事项的，需经您的主管税务机关批准。<br />
<br />
　　您如采取邮寄方式办理纳税申报，应当使用统一的纳税申报专用信封，并以邮政部门收据作为申报凭据。邮寄申报以寄出的邮戳日期为实际申报日期。<br />
<br />
　　数据电文方式是指我们确定的电话语音、电子数据交换和网络传输等电子方式。您如采用电子方式办理纳税申报，应当按照我们规定的期限和要求保存有关资料，并定期书面报送给我们。<br />
<br />
　　五、申请延期申报权<br />
<br />
　　您如不能按期办理纳税申报或者报送代扣代缴、代收代缴税款报告表，应当在规定的期限内向我们提出书面延期申请，经核准，可在核准的期限内办理。经核准延期办理申报、报送事项的，应当在税法规定的纳税期内按照上期实际缴纳的税额或者我们核定的税额预缴税款，并在核准的延期内办理税款结算。<br />
<br />
　　六、申请延期缴纳税款权<br />
<br />
　　如您因有特殊困难，不能按期缴纳税款的，经省、自治区、直辖市国家税务局、地方税务局批准，可以延期缴纳税款，但是最长不得超过三个月。计划单列市国家税务局、地方税务局可以参照省级税务机关的批准权限，审批您的延期缴纳税款申请。<br />
<br />
　　您满足以下任何一个条件，均可以申请延期缴纳税款：一是因不可抗力，导致您发生较大损失，正常生产经营活动受到较大影响的；二是当期货币资金在扣除应付职工工资、社会保险费后，不足以缴纳税款的。<br />
<br />
　　七、申请退还多缴税款权<br />
<br />
　　对您超过应纳税额缴纳的税款，我们发现后，将自发现之日起10日内办理退还手续；如您自结算缴纳税款之日起三年内发现的，可以向我们要求退还多缴的税款并加算银行同期存款利息。我们将自接到您退还申请之日起30日内查实并办理退还手续，涉及从国库中退库的，依照法律、行政法规有关国库管理的规定退还。<br />
<br />
　　八、依法享受税收优惠权<br />
<br />
　　您可以依照法律、行政法规的规定书面申请减税、免税。减税、免税的申请须经法律、行政法规规定的减税、免税审查批准机关审批。减税、免税期满，应当自期满次日起恢复纳税。减税、免税条件发生变化的，应当自发生变化之日起15日内向我们报告；不再符合减税、免税条件的，应当依法履行纳税义务。<br />
<br />
　　如您享受的税收优惠需要备案的，应当按照税收法律、行政法规和有关政策规定，及时办理事前或事后备案。<br />
<br />
　　九、委托税务代理权<br />
<br />
　　您有权就以下事项委托税务代理人代为办理：办理、变更或者注销税务登记、除增值税专用发票外的发票领购手续、纳税申报或扣缴税款报告、税款缴纳和申请退税、制作涉税文书、审查纳税情况、建账建制、办理财务、税务咨询、申请税务行政复议、提起税务行政诉讼以及国家税务总局规定的其他业务。<br />
<br />
　　十、陈述与申辩权<br />
<br />
　　您对我们作出的决定，享有陈述权、申辩权。如果您有充分的证据证明自己的行为合法，我们就不得对您实施行政处罚；即使您的陈述或申辩不充分合理，我们也会向您解释实施行政处罚的原因。我们不会因您的申辩而加重处罚。<br />
<br />
　　十一、对未出示税务检查证和税务检查通知书的拒绝检查权<br />
<br />
　　我们派出的人员进行税务检查时，应当向您出示税务检查证和税务检查通知书；对未出示税务检查证和税务检查通知书的，您有权拒绝检查。<br />
<br />
　　十二、税收法律救济权<br />
<br />
　　您对我们作出的决定，依法享有申请行政复议、提起行政诉讼、请求国家赔偿等权利。<br />
<br />
　　您、纳税担保人同我们在纳税上发生争议时，必须先依照我们的纳税决定缴纳或者解缴税款及滞纳金或者提供相应的担保，然后可以依法申请行政复议；对行政复议决定不服的，可以依法向人民法院起诉。如您对我们的处罚决定、强制执行措施或者税收保全措施不服的，可以依法申请行政复议，也可以依法向人民法院起诉。<br />
<br />
　　当我们的职务违法行为给您和其他税务当事人的合法权益造成侵害时，您和其他税务当事人可以要求税务行政赔偿。主要包括：一是您在限期内已缴纳税款，我们未立即解除税收保全措施，使您的合法权益遭受损失的；二是我们滥用职权违法采取税收保全措施、强制执行措施或者采取税收保全措施、强制执行措施不当，使您或者纳税担保人的合法权益遭受损失的。<br />
<br />
