																											<div id="postmessage_820611" class="t_msgfont">新首富宗庆后婉拒巴菲特邀请：光捐钱非真慈善<br />
2010年09月08日 21:48东南网-海峡导报<br />
东南网-海峡导报9月8日讯（本网记者 孙春燕 张顺和 曾毓慧 陈洋钦/文 黄少毅 张向阳 吴晓平/图）“很多人都怕做首富，但我觉得没什么，我不怕别人称我是首富，相反我觉得这是对我个人和企业价值的认同。”<br />
<br />
“以前做生意，多元化是必需的，东方不亮西方亮嘛，但近年来，企业只要多元化就是死路一条。”<br />
<br />
“对中国的民营企业来说，‘走出去’不是赶时髦，虽然全球各地都有华人的身影，但现在很多在国外的民企都是小打小闹，我认为，‘走出去’就是要在国外形成中国真正的市场，中国目前还没有叫得响的、真正崛起的世界品牌，我们娃哈哈就是要争取做中国的第一个世界级品牌。”<br />
<br />
“不管是荣誉，还是角色，我还是喜欢当人大代表，企业实业做大了，所担当的社会责任更重了；人大代表角色，能让我更好更实在地履行社会责任，在百姓、民生等方面向政府建言。”<br />
<br />
应投洽会组委会邀请，娃哈哈集团董事长宗庆后昨天参加了“浙商力量 闪耀海西”专场论坛。而本场论坛，也是宗庆后先生成为中国新首富之后的第一次公开亮相，备受参会嘉宾以及媒体的高度关注。<br />
<br />
昨日论坛结束后，宗庆后接受了本报记者的专访。<br />
<br />
谈首富 首富光环总统都拉他投资<br />
<br />
今年3月，金融危机的阴霾尚未完全退去，作为杭州娃哈哈集团掌门人，宗庆后以70亿美元的身家在福布斯财富榜中排名第103位，成为中国大陆新首富！<br />
<br />
“我白手起家、没搞钱权交易、没搞暴利行业，不介意被人称为首富，我也不怕当首富，这正是对我个人和企业价值的充分肯定，而且这让更多人有更多机会关注到我，关注到娃哈哈。”<br />
<br />
被冠以首富的光环之后，宗庆后的确感觉很受用―――举例而言，外国政府人员来向他招商的频率明显增加了。<br />
<br />
“前阵子，我就见了两次菲律宾总统，都是来推荐当地投资环车模颐且惨虼丝戳朔坡杀龅目蟛鸵弦怠！弊谇旌笏担」芸疾旃蠓⑾址坡杀龅哪笠闲幸刀疾皇呛苁屎贤蹲剩次薰哈迈向国际化提供了坦途，“至少人家是欢迎你的，人家是看得起你的，不像以前到外面去，人家根本不买你的账。”<br />
<br />
事实上，前来推销投资环境的远不止菲律宾，前来向宗庆后招商的国家名单中，还有瑞士、以色列、英国等，其中波兰政府人员还想趁着“九八”再和他谈一次<br />
谈慈善<br />
<br />
时间冲突婉拒巴菲特邀请<br />
<br />
成为首富以后，宗庆后还与慈善有了更深的关系。这不，比尔・盖茨和巴菲特本月底将到中国举办一场“慈善晚宴”，宗庆后就在被邀之列。<br />
<br />
“不过，我已经拒绝了，因为我要参加福布斯CEO大会，时间正好冲突了。你们不要误会啊，我可不是怕被劝捐，我只是没时间。”宗庆后说，他想捐不用劝也会捐，不想捐谁劝也没用，但从内心来说，他并不欣赏动辄就捐资产的慈善行为。因为在他看来，捐出全部资产的行为背后，不过是无法支付高额遗产税和企业避税的变通做法，并不是真慈善，只有持续地为社会创造财富才是真正的慈善。<br />
<br />
宗庆后说，企业家若把辛苦赚来的钱用来投资，就能产生更多的就业机会，能为国家和社会带来更多财政收入，但若是捐出去，这些资产就失去了“生命力”，“杀富济贫”是不对的。<br />
<br />
当然，作为一个年利税高达126个亿企业的老板，宗庆后当然也不会在灾难面前袖手旁观，不过按照他的话说“救急不救贫，救贫也要先造血”，所以除了在灾难后捐款外，娃哈哈一直坚持的是对口三峡扶贫、对口西部大开发等长期的慈善项目。<br />
<br />
“有钱人的钱多到一定的程度，这些钱就不是自己的，它应该是社会的，要为社会带来更高的综合收益。”在宗庆后眼里，他要坚持的，就是把娃哈哈做成世界一流的大品牌，真正的民族品牌，让更多的人因为娃哈哈的存在而生活无忧，这样才是大善。</div>

							
							
							
							
