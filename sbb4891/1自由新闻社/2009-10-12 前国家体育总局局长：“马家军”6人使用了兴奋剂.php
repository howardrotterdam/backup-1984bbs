													<h2>前国家体育总局局长：“马家军”6人使用了兴奋剂</h2>
						
						
																											<div id="postmessage_278255" class="t_msgfont">　昨天，袁伟民这位淡出公众视线已久的昔日体坛掌门人出现在南京，为他的新书《袁伟民与体坛风云》在凤凰书城举办了发布会。而更“生猛”的还属这本书中所写到的内容：“悉尼奥运会前夕铁腕处理‘兴奋剂’事件；北京申奥期间时任国际奥委会执委何振梁先生的真实做法。” <br />
<br />
　　坚持：我对事不对人<br />
<br />
　　书中一个极具爆炸性的披露是关于何振梁的。尽管没有指名道姓，但“国际奥委会某中国籍的资深委员”、“《五环之路》一书的作者”等指称无疑让公众把目光投向素有“体育外交家”、“中国申奥之父”名头的何振梁。该书披露，在申办北京2008年奥运会时，北京奥申委希望通过支持罗格(时任欧洲各国奥委会联合会主席)担任国际奥委会主席得到欧洲委员对北京举办2008年奥运会的支持票。但是该书引用袁伟民的话说：“但后来执行这个策略的时候并不是一帆风顺，一位国际奥委会资深的中国委员、北京奥申委的顾问，却意想不到地不配合，有些行为与北京申奥的总方针相悖，令我们费解、困惑和不安。”<br />
<br />
　　我们为了北京申奥成功，积极做中国的朋友的工作，希望他们也把票投给罗格。而这位国际奥委会资深的中国委员(指何振梁)却做反工作，让我们的朋友把票投给金云龙。他明明知道这么做对北京不利，为什么还要不听劝阻坚持己见呢？真不知道他的目的和动机究竟是什么？<br />
<br />
　　除此之外，书中还写到了审计风暴内有玄机、女排也玩假球这样的章节。袁伟民说，“我对事不对人。但提事肯定会涉及人。这是在所难免的。我只是不想再违背历史、违背真实了。不想让大家永远搞不清楚一些真相。总之，我不欠谁的。”<br />
<br />
　　艰难：自己肚子里咽<br />
<br />
　　袁伟民生命中有过许多华彩乐章的时刻，昨日有记者问他，最艰难的时刻是什么时候。袁伟民想了想说，那就是发生在2000年悉尼奥运会之前的“兴奋剂事件”。亚特兰大奥运会上风光无限的“马家军”为何忽然在2000年悉尼奥运会上销声匿迹，这在当时引起了全世界的猜测和热议，而这本书第一次揭开了此事的历史真相。<br />
<br />
　　当时“马家军”军团获得奥运会参赛权的7名队员中，2人尿检呈阳性(其中1名血检也超标)、4人血检超标，7人中有6人被证实使用了兴奋剂或者有强烈的使用兴奋剂嫌疑。最终以袁伟民为首的体育总局领导层决定将包括“马家军”在内的所有经抽查使用了兴奋剂或有使用兴奋剂嫌疑的运动员全部拒之悉尼奥运会之外。袁伟民说，“当时我真的很受打击，很难。这些我都没写在书里，我都往肚子里吞了。还好，最后坏事变好事了。得知中国在兴奋剂问题上的严格举措，时任欧洲各国奥委会联合会主席(现任国际奥委会主席)的罗格说：‘其他国家应该像中国一样在赛前抓出那些有欺骗行为的人。’”<br />
