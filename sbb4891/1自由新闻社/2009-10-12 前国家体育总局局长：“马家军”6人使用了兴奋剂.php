　　这天晚上，在中国收视率最高的中央电视台《新闻联播》和《晚间新闻》中，都播出了一条重大新闻：当天在第十届全国人民代表大会常务委员会第十次会议上，国家审计署公布了“关于2003年度中央预算执行和其他财政收支的审计工作报告”。这是一个数万字的大报告，但这条新闻重点披露了审计报告“对审计55个中央部门和单位2003年度预算执行中查出的突出问题”，指出：“41个部门挤占挪用财政专项拨款和其他有专项用途的资金14.2亿元，主要用于建设职工住宅、办公楼和发放各类补贴。如1999年以来，国家体育总局动用中国奥委会专项资金1.31亿元，其中用于建设职工住宅小区1.09亿元。”国家体育总局作为41个部门挤占挪用财政专项拨款和专项资金的典型例子被突出曝了光，成为这条新闻的焦点，所以央视在播出这条新闻时，所用的背景画面，都是龙潭湖畔中国体育大本营办公楼前那块醒目的牌子――“国家体育总局”以及龙潭湖职工住宅小区。<br />
　　一夜之间，突如其来的审计风暴，把国家体育总局送到了风口浪尖上。<br />
　　中国唯一的中央级英文报《中国日报》又将这则消息用英文刊出：国家体育总局挪用了搞2008年奥运会的资金建职工住宅。而新华社在对外发这则消息的英文通稿时，又译成：挪用了这个国家的奥林匹克专项资金建职工住宅。由此，又引来了境外媒体大炒作，外电明确说：“根据中国官方喉舌报道，国家体育总局挪用了搞2008年奥运会的资金。”法新社、美联社、英国《金融时报》等海内外许多媒体开始以各种严厉的措辞批评国家体育总局“挪用了‘2008年奥运会资金’、‘中国奥组委资金’、‘备战奥运会资金’”，“贪污了‘纳税人的钱’和‘社会各界的捐赠’”等等，有的媒体的指责甚至上升到：体育总局挪用奥委会资金、北京奥运会资金建了机关干部的“腐败楼”、“贪官楼”。由此激怒了人民群众和海外华人，发出了“我们再也不给奥运会捐赠了”的怨言。<br />
　　一夜之间，国家体育总局被炒向了全国、炒向了世界。整整两个多星期，海外媒体和互联网上这类报道铺天盖地，受到审计风暴突袭的国家体育总局顿时名声扫地。而这时，中国体育的大本营，正紧锣密鼓地备战，迎接即将在雅典拉开帷幕的第28届奥运会。<br />
　　袁伟民说：具有中国特色的社会主义机关干部和事业单位职工住房，过去都是由国家分配的，国家投资建房，然后将国家的房，让职工付低租金入住，按国家的有关规定，不同级别的职工入住不同面积的房。1999年和2000年时，国家开始进行房改了，采取国家的房子低价卖给职工，以后逐月发放住房补贴的办法，实现住房商品化了。可是，有许多老单位，因为员工多，欠账太多，有许多员工还根本没分配到住房呢，没有那么多老房子可卖给职工，怎么办呢？所以，当时国家给了一个临时政策，允许在那一阶段（也就是1999年、2000年那两年）可以采取国家（自有资金）、集体（单位）、个人自筹集资建房。<br />
　　这是一个为民解忧、雪中送炭的政策，所以当时体育总局党组研究认为一定要抓住这个机会，如果不搭上这最后一班车给群众建房，就很难解决体育总局系统无房户、未达标户过多的历史遗留问题了。<br />
　　袁伟民分析说：问题出在审计署公布的报告中，把我们建房动用的中国奥委会市场开发积余的钱，误定性为动用了中国奥委会专项资金，多了“专项”这两个字，这就使问题性质发生了变化。懂经济、懂财务的人都知道，“专项资金”是财政专用名词，有特定的涵义，不能随意扩大使用。正因为误把这笔资金定性为“专项资金”，所以被划到了“中央预算执行和其他财政收支的审计报告”中去。事实上，中国奥委会的账户上既没有财政专项拨款，也没有其他有专项用途的“专项资金”，只有市场开发积余的钱，这些钱是10多年来各单项协会和中国奥委会进行市场开发积累下来的，主要是八九十年代运动员、运动队做广告所得，以及参加国际奥委会、亚奥理事会和国际单项协会组织的比赛和大奖赛的分成，因为当时运动项目管理中心还没有成立，思想观念也没有那么开放，也没有什么相关的分配政策，所以广告费和奖金也没分掉，其中还包括我当教练时带领中国女排打比赛分得的钱和中国女排做广告的钱在内呢！这些中国奥委会市场开发积累的钱，都是运动员、教练员、体育工作者艰苦拼搏的血汗钱。取之于他们、用之于他们，应该说也算合理。<br />
　　袁伟民说：这类钱的来源存在很大的不确定性，运动成绩好，市场开发就好，就有，就多得；反之，就没有或少得。既非国家财政拨款也非专项资金。但是审计公报明文公布的是国家体育总局动用了中国奥委会的专项资金，并以此作为中央41个部委中“挤占挪用财政专项拨款和其他有专项用途资金”的典型例子予以公布。所以海内外媒体与广大受众很自然地以为国家体育总局动用了国家财政拨给中国奥委会的有专门用途的资金，占用了“举办2008年奥运会的资金”、“备战奥运会资金”、“建设体育场馆的资金”等等。一夜之间刮起的审计风暴愈演愈烈，引起轩然大波。</div>

							
							
							
							
