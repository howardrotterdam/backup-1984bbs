人民网・中国共产党新闻网正式推出“直通中南海――中央领导人和中央机构留言板”。<br />
<br />
<br />
　　本报讯 9月8日，人民网・中国共产党新闻网正式推出“直通中南海――中央领导人和中央机构留言板”。该留言板突出互动性，旨在让广大网友对中央领导人倾诉心声，给中央机构提出意见和建议。<br />
<br />
<br />
　　“直通中南海――中央领导人和中央机构留言板”以中南海新华门和“为人民服务”影壁动画页面为封面，网友点击进入留言板首页就可以给中央领导人和中央机构留言。网友留言可以个人的“网友”身份，也可以“党组织”或单位名义；同时，网友可以查看所有留言，也可以就其他网友的留言进行点评。留言板还鼓励网友通过人民网手机平台参与互动交流，留下意见和建议。<br />
<br />
　　除了留言功能外，“直通中南海”留言板首页还设置了更多可读性很强的互动栏目，将中央领导人与网友交流的有关新闻、中央机构推动党务公开的最新信息加以展示。<br />
<br />
　　房价和教育问题最受关注<br />
<br />
　　记者昨日在中国共产党新闻网“直通中南海”留言板上看到，自9月8日该网正式推出“直通中南海――中央领导人和中央机构留言板”以来，给胡锦涛总书记和温家宝总理的留言都超过1万条。<br />
<br />
　　记者对最近的5页留言统计后发现，网友给胡锦涛总书记的留言反映高房价的最多，这类留言占三成。而给温家宝总理的留言更多的是反映教育问题，也占了近两天留言的三成。<br />
<br />
　　网友在给胡锦涛的留言中，“房价”成了最热门的话题，“降房价”成了许多网友的心声。甚至有些网友直接建议，国家继续对房地产实行高压政策，调控房价回落到2005年的水平上。<br />
