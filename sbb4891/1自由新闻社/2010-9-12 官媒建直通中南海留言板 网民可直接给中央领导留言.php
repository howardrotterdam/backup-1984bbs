中国共产党新闻网推出直通中南海留言板<br />
<br />
9月8日，人民网・中国共产党新闻网正式推出“直通中南海―――中央领导人和中央机构留言板”。该留言板突出互动性，旨在让广大网友对中央领导人倾诉心声，给中央机构提出意见建议。截至昨晚8点，给胡锦涛总书记的留言共12024条，给温家宝总理的留言，共9122条。<br />
<br />
