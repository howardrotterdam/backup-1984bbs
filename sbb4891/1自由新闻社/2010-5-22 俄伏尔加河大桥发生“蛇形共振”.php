																											<div id="postmessage_633624" class="t_msgfont">【塔科玛桥风毁事故与卡门涡街】<br />
　　20世纪40年代，美国塔科玛峡谷桥（Tacoma Narrow Bridge）风毁事故的惨痛教训，使人们认识到卡门涡街对建筑安全上的重要作用。 　　1940年，美国华盛顿州的塔科玛峡谷上花费640万美元，建造了一座主跨度853.4米的悬索桥。建成4个月后，于同年11月7日碰到了一场风速为19米/秒的风。虽风不算大，但桥却发生了剧烈的扭曲振动，且振幅越来越大（接近9米），直到桥面倾斜到45度左右，使吊杆逐根拉断导致桥面钢梁折断而塌毁，坠落到峡谷之中。当时正好有一支好莱坞电影队在以该桥为外景拍摄影片，记录了桥梁从开始振动到最后毁坏的全过程，它后来成为美国联邦公路局调查事故原因的珍贵资料。人们在调查这一事故收集历史资料时，惊异地发现：从1818年到19世纪末，由风引起的桥梁振动己至少毁坏了11座悬索桥。 　　第二次世界大战结束后，人们对塔科玛桥的风毁事故的原因进行了研究。一开始，就有二种不同的意见在进行争论。―部份航空工程师认为塔科玛桥的振动类似于机翼的颤振；而以冯卡门为代表的流体力学家认为，塔科玛桥的主梁有着钝头的H型断面，和流线型的机翼不同，存在着明显的涡旋脱落，应该用涡激共振机理来解释。冯&#8226;卡门1954年在《空气动力学的发展》一书中写道：塔科玛海峡大桥的毁坏，是由周期性旋涡的共振引起的。设计的人想建造一个较便宜的结构，采用了平钣来代替桁架作为边墙。不幸，这些平钣引起了涡旋的发放，使桥身开始扭转振动。这一大桥的破坏现象，是振动与涡旋发放发生共振而引起的。 　　20世纪60年代，经过计算和实验，证明了冯&#8226;卡门的分折是正确的。塔科玛桥的风毁事故，是一定流速的流体流经边墙时，产生了卡门涡街；卡门涡街后涡的交替发放，会在物体上产生垂直于流动方向的交变侧向力，迫使桥梁产生振动，当发放频率与桥梁结构的固有频率相耦合时，就会发生共振，造成破坏。 　　卡门涡街不仅在圆柱后出现，也可在其他形状的物体后形成，例如在高层楼厦、电视发射塔、烟囱等建筑物后形成。这些建筑物受风作用而引起的振动，往往与卡门涡街有关。因此，现在进行高层建筑物设计时都要进行计算和风洞模型实验，以保证不会因卡门涡街造成建筑物的破坏。据了解，北京、天津的电视发射塔，上海的东方明珠电视塔在建造前，都曾在北京大学力学与工程科学系的风洞中做过模型实验。</div>

							
							
							
							
