																											<div id="postmessage_762575" class="t_msgfont"><img src="http://gb.cri.cn/mmsource/images/2010/07/28/96/4562629456313904884.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
望着妻子痛苦不堪的样子，陈先生很是无奈。<br />
<br />
<br />
<img src="http://gb.cri.cn/mmsource/images/2010/07/28/16/11815054354538481476.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
事发地凤凰医院<br />
<br />
<br />
　　“你准备好了吗？你的儿子是我接生，他出生后第一眼看到的就是我，你要非常重视这件事啊。”<br />
<br />
　　――产妇待产期间，张助产士4次暗示家属<br />
<br />
　　“哼，你们这样的家属我见得多了，你不要跟我说这些，我并不是贪钱的。”<br />
<br />
　　――家属给了100元红包，并答应事后再给1000元，张助产士如是说<br />
<br />
　　“你打我两巴掌解解恨吧，你妻子的住院费用你就不要担心了，我也是好心给她做痔疮手术的。”<br />
<br />
　　――“缝肛门”事发两天后，张助产士悄悄对家属说<br />
<br />
　　深圳新闻网7月28日讯 “助产士竟把我老婆的肛门给缝上了，这样的助产士太黑心了！”虽然事情过去了几天，但昨天下午记者采访时，陈先生提起这事，仍气愤不已，他怀疑是助产士嫌红包少而故意这样做的。涉事的罗湖区凤凰医院相关负责人称，对于发生这样的事情，现医院正着手调查，并向患者道歉。<br />
<br />
　　“助产士曾4次暗示要‘表示’”<br />
<br />
　　陈先生告诉记者，他家住在罗湖区黄贝岭社区，他的妻子小红（化名）去年11月怀孕后，由于家附近有一家凤凰医院，他们就把产前检查定在了这家医院。“产前检查没有发现这家医院有什么问题，没想到妻子生产时竟会遇到这样的事情，真是太不可思议了。”陈先生说。<br />
<br />
　　陈先生称，本月23日上午8时许，怀孕10个月的妻子有了产前征兆，于是他就把妻子小红送到了凤凰医院妇产科待产。陈先生说，在等待期间，一名40多岁、姓张的女助产士曾4次对他说：“你准备好了吗？你的儿子是我接生，他出生后第一眼看到的就是我，你要非常重视这件事啊。”陈先生心想，产妇都在她面前了，还要准备什么啊？要准备也是医院准备接生才对啊。想了一会儿，陈先生恍然大悟了，助产士问他准备好了吗的意思是不是想要红包啊？<br />
<br />
　　陈先生告诉记者，由于出来时匆忙，身上只带了200元现金。他对这名助产士说：“你的意思我懂，但是我出来的时候非常匆忙，身上没有太多的现金，只带了卡，这个你放心，规矩我还是懂的，我一定会给你的。”说完，陈先生把100元现金用纸包着给了这位助产士。“张助产士拿了钱就把妻子推到待产室去了，但回来的时候脸色十分难看，我估计她嫌红包少了。就又对她说，等妻子生产完了一定给她一个1000元的大红包。”陈先生再次强调说。<br />
<br />
　　“哼，你们这样的家属我见得多了，你不要跟我说这些，我并不是贪钱的。”张助产士狠狠瞪了陈先生一眼，去了产房。陈先生还告诉记者，他也试图把另外一个100元的红包给接生的薛医生，但他拒绝了。<br />
<br />
　　半夜发现妻子肛门被缝<br />
<br />
　　陈先生告诉记者，当天下午3时45分许，妻子顺产了一个体重5.5斤的男婴，孩子出生后三项体征的评分有两个是10分，一个是9分，医生称，这样的分值证明孩子是十分健康的。<br />
<br />
　　陈先生告诉记者，妻子生产时被实施了椎管内麻醉，生产后没觉得身体有什么不舒服就回到病房了。到了半夜，妻子直喊肛门疼。起初，陈先生和妻子都不在意会有什么问题，但妻子确实难以忍受时，陈先生一看这才大吃一惊，他发现妻子的肛门已经肿得有拳头那么大，而且还用黑线缝着。于是，陈先生就去找薛医生，薛医生称，他根本没有给小红做肛门缝合手术。陈先生再去问张助产士时，她承认见小红有痔疮，她顺手给她做了个手术。<br />
<br />
　　陈先生称，也许是害怕自己做错了事，会受到医院处理，25日，张助产士悄悄把陈先生叫到一个房间对他说：“你打我两巴掌解解恨吧，你妻子的住院费用你就不要担心了，我也是好心给她做痔疮手术的。”陈先生告诉记者，她的妻子从来就没得过痔疮，哪来的这个病。即使是妻子有痔疮，一个助产士也没有资格做手术啊，为什么不报告给医生做呢？“我怀疑，她是嫌我给她的红包少了，她借机把我妻子的肛门给缝上了。”陈先生气愤地说。<br />
<br />
　　更让陈先生和她妻子难以接受的是，他们儿子出生第二天得了肺炎，被转到了深圳市人民医院治疗。张助产士趁陈先生不在，不顾小红的疼痛叫喊，强行把肛门缝合的线给拆除了，还称要把缝合的部分切除掉。“她这样做是想毁掉证据，我现在还能看到好几个针眼，她只承认缝了一针，作为一个助产士，连起码的职业操守都没有，怎么救死扶伤呢？”陈先生气愤地说。<br />
<br />
　　院长拒绝接受采访<br />
<br />
　　凤凰医院一位自称是吴主任的女工作人员，在接受其他媒体采访时称，对于此事现在医院正着手调查。她说，他们医院在招聘工作人员这方面很不专业，不像公立医院招的人员那么好，出现这样的情况，她代表医院给患者家属道歉。昨天下午，记者采访该院王主任时，他称有很多不便拒绝接受采访。随后，记者又找到了该院的赵院长。赵院长以患者家属没有同意采访为由，也拒绝了记者的采访。<br />
<br />
　　□业内人士说法<br />
<br />
　　助产士无权做手术<br />
<br />
　　深圳市人民医院、深圳第二人民医院、深圳急救中心的资深医生和护士长在接受记者采访时称，不管什么原因，助产士是没有权力给产妇做任何生产之外的手术。“即使产妇有其他疾患，助产士也只能把情况报告给医生，她是没有权力进行处置的。”深圳人民医院一名姓杜的医生说。<br />
<br />
