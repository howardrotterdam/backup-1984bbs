按照国外的标准，本国的左其实是右，右其实是左。ZZ倾向只有两种，一种是倾向于集权，一种倾向于分权。极左就是无政府，极右就是毒裁。基本上所有的西方政权都是中间偏左，或中间偏右。 </blockquote></div>就我目前认知程度来讲,中国的左右派与民主国家(比如美国)的左右派之分有着重要的区别.<br />
比如在美国,从经济角度来看,右派主张社会高福利,更公平的或者说更平等的分配蛋糕(GDP).即在&quot;公平与效率&quot;之间更侧重于前者,而左派主张市场竞争,他们在&quot;公平与效率&quot;之间更侧重于后者,即把蛋糕做大(GDP).而从政治角度来看,右派主张&quot;大政府&quot;,认为政府应该拥有更多的权力,这样才能更好的服务社会,左派则相反.总的来说,右派更看重伦理道德以及政府的作用,而左派则更侧重个人自由.比如在堕胎的问题上,右派认为这种行为是一种谋杀,应该受道德遣责甚至是法津制裁,左派则认为堕胎完全是一种个人的自由.当然,强行堕胎根本不在左右两派的探讨范围内.但人是很复杂的,所以左右派的划分不是绝对的.比如有的人虽然主张市场竞争,在经济上是右派,但在堕胎的问题上却又认为堕胎是一种谋杀.<br />
总之,美国的左右两派之争虽然看上去十分激烈,但他们都有着共同的价值观:民主,自由,宪政和人权.他们之间根本不存在生死存亡的斗争.他们都认同宪法,都崇尚这个国家最根本的自由理念.最大的区别只是在于思考方式的不同.<br />
<br />
中国的左右两派则完全不同.<br />
我在以前的贴子里说过,当今中国的左派分为真左和假左,前者对毛泽东思想和共产主义理论推崇备至,而且主张&quot;大政府&quot;和&quot;贡献&quot;.他们要求人们有崇高思想道德觉悟,用伦理道德的力量来约束自己,贡献自己.正所谓&quot;狠斗私字一闪念,灵魂深处闹革命&quot;.<br />
在毛腊肉时代,其经济的基本特征就是&quot;全民所有制&quot;,而这种经济体又需要一个强有力的政府来主导,这就需要集权集权再集权.实践已经证明,这种发展方式给中国带来了极大的灾难.集权的结果就是特权.而特权又是与人权相对立的,从这个方面也可以证明毛太祖时代和现在的中国没有人权,只是程度不同而已.<br />
当今中国的右派则崇尚西方国家的基本理念:民主,宪政,自由和人权.他们认为只有民选的政府,以及这种政府只有在宪政的条件下(严格限制政府权力)才能更好的防止官僚特权阶级的出现,才能更好的保障个人权利不受侵犯.<br />
<br />
总的来说,美国左右两派是在共同价值观的理念下,由于思考方式和角度的不同,所产生的分歧.而今日中国的左右两派看上去火药味十足,呈现出水火不容的态势,我认为就是因为基本的价值观不同----尽管他们都痛恨腐败----但在对待这个问题上,左派会用无产阶级专政下的继续革命的理论来号召民众打倒官僚特权阶层(尽管这个阶层在左派的理论中注定了是一个不死之身),右派则提出了民主宪政的观点来表达自己.<br />
<br />
PS:我个人认为,倘若今日之中国的左派再次得势,他们将再次高举&quot;无产阶级专政&quot;的破旗继续革命,而且他们并不会包容异已者发出的声音,这也是毛腊肉时代的特征之一------黑暗中除了听见一只怪兽在嗷嗷乱叫以外,很难再听不到别的声音了.(如遇罗克,张志新,储安平,章伯钧,罗隆基,季羡林,梁漱溟等)<br />
若是今日之中国右派得势,由于他们深知言论自由的重要性,因此他们仍然会包容异已者的声音,包括左派的声音,哪怕左派的声音有可能导致不自由的时代再次降临.因为他们知道这样一句话:如果你因为害怕一个不自由的时代,因此就不给他们言论自由的话,那么,这个不自由的时代就已经开始了.是你自己给它开了头.<br />
