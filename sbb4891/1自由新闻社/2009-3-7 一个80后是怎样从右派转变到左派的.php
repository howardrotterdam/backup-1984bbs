													<h2>一个80后是怎样从右派转变到左派的</h2>
						
						
																											<div id="postmessage_46921" class="t_msgfont">[转贴]郭小娟：一个80后是怎样从右派转变到左派的<br />
<br />
文章提交者：谭培明 加帖在 猫眼看人 【凯迪网络】 <a href="http://www.kdnet.net" target="_blank">http://www.kdnet.net</a><br />
<br />
从懵懂的右派到坚定的左派，一个80年后的精神转变之路　　　　<br />
<br />
郭小娟　 　　　<br />
<br />
&nbsp; &nbsp; 本人是80年后。在工作上和生活中，我见识了很多让我深深认同右派评判性言论的事情。就在去年和07年，我以文武几的昵称在搜狐社区发了很多帖子。看看我发在搜狐社区的文字就知道，就在去年，我还是一个右派，也因此结识搜狐社区右派中一些正直的人物。我因那些正直人物的存在，而更加觉得右派可靠。　　　　<br />
<br />
&nbsp; &nbsp; 但是，很明显的社会现象和具体事实摆在面前，通过这些社会现象和具体事实，可以分析得出这个世界的真相。而就在我逐渐对于右派心存疑虑的时候，发生了一件最终决定了我思想转变的事情。我儿子因为喝了雅士利奶粉而患上了肾结石。当时，我感到了恼怒、感到了真实存在的愤恨。去医院检查，是免费。而我儿子，通过医生建议的在家进行的保守治疗手段，很快也得到康复。我心中稍感宽慰。但是，这个事情对于我个人心理上的打击与伤害，无疑是存在的。　　　　<br />
<br />
&nbsp; &nbsp; 在分析一些社会现象的时候，我开始变得更为理智。在读书的时候，我开始接触更多的我从前不屑一顾的书籍。在上网的时候，我也开始留意xx之x。我力图更加全面、客观的认识这个世界。　　　　<br />
<br />
&nbsp; &nbsp; 我最终清醒的认识到，右派总是被利用。　　<br />
<br />
&nbsp; &nbsp; 在右派被利用的背后，当然是有着一拨势力的。那拨势力，正是妄图通过经济专制来统治世界的阴谋集团。他们的策略是：通过私有制度下必然迅速到来的不受监控的权力本身来诱惑腐蚀干部，将中国的实体经济以化公为私的方式全部搞垮或通过所谓的私有化贱卖，同时将中国演变为一个权力犯罪的天堂。我发现，如果依据几只披着羊皮的狼来否定羊圈里所有的羊，甚至是否定羊这一种动物种群的可靠性，并宣扬要将羊杀光，那肯定是极端错误的。<br />
<br />
&nbsp; &nbsp; 右派依据已经给出的所谓真相，但事实上，包括互联网在内的公众媒体都不过是一种舆论工具,被怀有各种目的的集团与群体操纵、摆弄、利用。对于所谓真相的控制能力，成为有权势者所掌握的权势的一部分。作为弱势群体的我们，总是掌握不了真相，最多掌握所谓的真相，是权势掌握者提供给我们的所谓的真相。比如：杨佳杀死了六个警察，并且被处死，这都是真相。但是，问题在于，那不过是已经提供给我们的真相的一部分。还有相关于这个真相的另外一部分，我们只能猜测。到底杨佳早前有没有被殴打或侮辱，我们对此只能猜测。这才是分辨真相的时候所必备的思维。现实，在我们毫不知情或不愿相信的情况下，甚至有可能比恐怖小说更恐怖。非官方历史的重要性，古人就已经意识到了。但右派们所依据的非官方历史，如所谓伤痕文学啊、右派回忆录啊什么的，不过是非官方历史中难辨其真假的一部分。关于二战，关于大跃进，关于文革，我们所知道的，也只是其中的一部分，另一部分，是被刻意或无意掩盖、遮蔽了的，或是因不符合利益需要与时势需要而鲜为人知的。我从不完全认同由政权所给出的历史，也从不完全依靠媒体舆论所给出的信息来判断真相。同时，也很审慎的对待有时似乎更为可信的脱离政权影响的个人的历史。　　<br />
<br />
&nbsp; &nbsp; 我知道，我能肯定的是：右派所梦想、欢呼并正如其所愿到来的，不过是一个资本权力取代政治权力更为残酷而无情的掠夺、奴役地球弱势人群的时代，是一个地球财富资源被极少数人掠夺为己有并合法化的时代，是一个弱势人群本身也最终沦落为被任意支配、玩弄、利用的物化的资源的时代。那绝对不该是中国人所当选择的未来。　　<br />
<br />
&nbsp; &nbsp; 从前，右派说左派被洗脑，后来，左派说右派被洗脑。到底是谁被洗脑？人类历史上，其实真正清醒的，只是极少数人。大多数人都一直处于被洗脑的状态，并懵懵懂懂的被奴役被欺压了一生而不自知。　　　　<br />
<br />
&nbsp; &nbsp; 这个世界似乎总是道高一尺魔高一丈，不知道是不是因为魔鬼的手段更厉害。历史上有过的，也不过是以魔制魔。当以魔制魔失败，成功的魔鬼便再一次利用失败的魔鬼的臭名昭著来确立自己的威信、巩固自己的统治地位。纵观20世纪的地球历史，摆在地球人面前的，是三个巨大的魔鬼。一个魔鬼叫做极端民族主义，一个魔鬼叫做政治强权，一个魔鬼叫做经济强权。极端民族主义曾经是对抗经济强权的最大敌人，很快就被打败。接着，政治强权成为对抗经济强权的最大敌人。而现在，经济强权几乎已经打败了所有最大的敌人而开始着手建立一种人类有史以来最为残酷的社会秩序。事实上，包括美国在内的所谓民主政权，都与黑社会、霸占主要经济资源的恶“党”有着纠葛不清的关系。如果说伊斯兰世界是政教合一，则所谓民主政权不过是黑、金、政合一。如果一味仿效学习美国，中国的未来不容乐观。　　　　<br />
<br />
&nbsp; &nbsp; 分析中国目前的境况，只能说，是很可悲的。　　　　<br />
<br />
&nbsp; &nbsp; 我只能悲观的看见：人们在两个魔鬼之间踱来踱去。一个魔鬼拉了一部分人，另一个魔鬼拉了另一部分人。一部分人说：这个魔鬼是好的，另一个魔鬼是坏的。另一部分人说：那个魔鬼是好的，这个魔鬼是坏的。这两部分人吵了起来，甚至打了起来。　　　　<br />
<br />
&nbsp; &nbsp; 我们能够通过死亡来实现生存的目的吗？能够通过报复性的杀戮来实现公平与正义吗？能够通过喝农药来实现治疗疾病的愿望吗？如果一定要同意并实施，那显然是才是真正的邪教的行为方式和思维方式。　　　　<br />
<br />
&nbsp; &nbsp; 美国就是一个经济强权的国家典型。其经济强权，除针对国内，还针对国际。我对那样的国家，没有好感。　　　　<br />
<br />
&nbsp; &nbsp; 一百多年以前，马克思就向我们揭示了经济强权这个魔鬼的本来面目。现在，这个魔鬼已经露出了其狰狞的面目，很多人却还要否定其魔鬼的本质。　　　　<br />
<br />
&nbsp; &nbsp; 右派，不过是经济强权这个魔鬼的仆从，不过是被这个魔鬼利用了而已。　　　　<br />
<br />
&nbsp; &nbsp; 左派的本质，不是维护政治强权，也不是掩护经济强权，更不是宣扬极端民族主义，而是批判并消灭所有的魔鬼。<br />
<br />
&nbsp; &nbsp; 我认识到了经济强权等魔鬼的本质。于是，我成为了一个坚定的左派。我永远不相信经济强权这个魔鬼，我也不属于政治强权那个魔鬼。同时，我很厌恶极端民族主义。　</div>

							
							
							
							
