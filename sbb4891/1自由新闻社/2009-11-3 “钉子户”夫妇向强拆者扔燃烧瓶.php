　　德国用坦克进攻西班牙时，西班牙人拿不出更好的对策，在绝望中他们只好跟坦克打交手战。人们躺在壕沟或堑壕内，等坦克快要开到他们身体上方时，他们突然跳了起来，爬到坦克顶部，对着观察孔向车内射击，或者用撬杠撬开乘员舱口盖板或火炮发射孔板，然后向里射击。有的战士向发动机内倾注汽油，把它点燃。在这种交手战中，西班牙人发明了一种极其简易的反坦克武器，叫莫洛托夫鸡尾酒瓶炸弹。几乎人人能做，家家可以制造，这种武器多少给德国的坦克带来了威胁。什么是 “莫洛托夫鸡尾酒瓶炸弹”呢？就是在酒瓶内装上汽油、水和黄磷的混合物，同时还要附一块胶皮。胶皮和挥发油起作用后，可以生成一种粘性胶体。一旦见到敌人坦克来了，就使劲摇动酒瓶，然后向坦克投击。一旦瓶子撞碎在坦克上之后，流出的黄磷在空气中立即燃烧，火焰很猛烈。这种武器本身不能把坦克击毁，但往往给坦克造成极大的破坏。燃烧的浓烟很快会进入坦克乘员舱内，滚滚浓烟首先引起驾驶人员的惊慌，怕发生爆炸，总是赶快停止操作，从舱口向外爬出来。这个时候就很容易被反坦克手用枪打死，当时坦克都采用汽油，而且油箱都在外部，莫洛托夫鸡尾酒瓶的炸弹一燃烧，往往把坦克油箱点燃，然后引起坦克燃烧，失去油料而停止运动。当然莫洛托夫鸡尾酒瓶炸弹在运用上是危险的，跟坦克距离很近，具有冒险性。但在没有办法时想出这招，这不能不说是西班牙人的一种创造。</div>

							
							
							
							
