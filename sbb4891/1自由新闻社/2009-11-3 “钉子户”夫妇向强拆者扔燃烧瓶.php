																											<div id="postmessage_311822" class="t_msgfont">燃烧弹大体分为两类,点燃式和触发式.点燃式较为常见,最有名的应该是,它的配方如下:<br />
　　<br />
　　1.三分之二的汽油与三分之一的糖混合.<br />
　　<br />
　　2.三分之一的洗衣粉和三分之二的汽油,再加上少量其他油料.<br />
　　<br />
　　引：<br />
　　<br />
　　燃烧瓶，又称「莫洛托夫鸡尾酒」，是游击队等非正规部队、街头暴乱群众的常用武器，军方亦有采用，有些罪犯也会以它来纵火，不同于制式武器烧夷弹（燃烧弹）。<br />
　　成份<br />
　　<br />
　　燃烧瓶的主要构造为：玻璃瓶，瓶内装有半满的易燃液体，例如汽油或酒精（甲醇或乙醇）；瓶口以水松或塑胶、橡胶等不透气塞堵住；瓶口上扎上布块作引。使用之前把布块沾透易燃液体，燃点布块后把瓶抛出。玻璃瓶撞击目标破裂，易燃液体倾倒在目标之上，被火点著燃烧，可造成一定的阻绝及杀伤能力。<br />
　　<br />
　　为了加强武器的效果，可以在瓶内加入其他物料(可选):<br />
　　<br />
　　* 自燃物质，例如白磷，以确保击中目标后会著火。<br />
　　* 焦油、橄榄油、洗衣粉、洗洁精、面粉等可令燃料浓度增加变稠附在目标上面。<br />
　　* 松节油、油漆、塑胶袋丝产生大量浓烟<br />
　　* 强酸，例如盐酸，用来增加杀伤力，或腐蚀防火的表层。<br />
　　* 可加入少许啤酒罐裁成的小铝片增加燃烧温度<br />
　　<br />
　　燃烧瓶的原理跟正规军队使用的燃烧弹基本相同；两者都是以汽油混合使汽油变稠的物料。<br />
　　<br />
　　名称由来<br />
　　<br />
　　燃烧瓶的别名「莫洛托夫鸡尾酒」来自苏联的外交人民委员（外交部长）维亚切斯拉夫・米哈伊洛维奇・莫洛托夫。二次大战时苏联入侵芬兰，芬兰士兵无论是人数或装备皆处於下风。面对红军的坦克，芬兰人借用了之前在西班牙内战时，这种由苏联人支持的共和派人发明的燃烧瓶武器。<br />
　　<br />
　　在苏芬战争中，苏联轰炸机曾用燃烧弹轰炸芬兰的城市和平民目标。面对国际社会的指责时，莫洛托夫宣称苏联飞机没有向芬兰人民扔下炸弹，而是向他们空投面包。芬兰军民便将苏联燃烧弹称为“莫洛托夫面包篮”，并将他们准备的这种燃烧瓶称为莫诺托夫鸡尾酒，用来招待苏联坦克。芬兰军方在之后持续的战事中大量生产燃烧瓶，以手或掷石器投掷。<br />
　　<br />
　　二次大战中参战各方都有使用燃烧瓶作为防守的武器，它对於阻挡轻坦克特别有效。1948年以色列和阿拉伯战争中的以色列、1956年匈牙利事件中的匈牙利t众，都曾成功使用燃烧瓶阻止敌人装甲兵的前进。<br />
　　<br />
　　坦克车的底部往往是防御最少的，当燃烧瓶在坦克的尾部燃烧时，可能令坦克的油缸爆炸。部分苏制坦克设有外挂油缸，更可以从外面被打开，很易被燃点。当游击队在近距离以燃烧瓶快速突击，坦克车的灵活性不足应付，於是陷入被动。这亦是燃烧瓶在当时能成为成功武器的原因之一。<br />
　　<br />
　　常见错误<br />
　　<br />
　　由於燃烧瓶制作容易，使它几乎已成为游击队及街头暴民的「标准装备」。但对全无经验的人来说，制造及使用燃烧瓶亦非全无难度。第一种常犯的错误是瓶内放进太多的汽油，在玻璃瓶碎开后过多汽油会难以著火，正确的做法是放进1/2至2/3的汽油。另一种错误是点燃布块前没有把玻璃瓶抹乾净，使瓶外残余的汽油燃烧起来。此外，用作引信的布块来封著玻璃瓶也是错误的作法。其他各种错误包括瓶子的塞不密封，选用错误（太坚固或太易碎，瓶颈太长或太宽）的玻璃瓶等等。但最常见的错误还是点燃以后的投掷过程出错。<br />
　　<br />
　　燃烧瓶虽说是制作简单，但对不熟悉的人来说它还是存在一定的危险。<br />
　　<br />
　　<br />
　　<br />
　　其他种类自制燃烧弹配方.<br />
　　<br />
　　氯酸钾(某些润喉片含有它)和糖按3:1混合具有惊人的燃烧力.<br />
　　<br />
　　高锰酸钾和糖按9:1混合也可以,但不如上一个配方易燃.<br />
　　<br />
　　氯酸钾(除草剂的主要成分)和糖按3:1混合使用.<br />
　　<br />
　　硝酸钾加糖不需引燃撞击就可以自然发火.<br />
