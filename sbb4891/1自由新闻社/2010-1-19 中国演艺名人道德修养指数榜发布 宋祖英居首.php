<br />
相比之下，获全国“五一”劳动奖章的苏州市滑稽剧团台柱兼名誉团长顾芗出人意料地垫底，排在了负面新闻不断的艺人周杰、陈冠希等后面。满意度调查组委会有关人士表示，这一排名并不说明顾芗的道德修养比其他艺人差，因为指数数据实际体现的是公众对艺人的期待值。<br />
