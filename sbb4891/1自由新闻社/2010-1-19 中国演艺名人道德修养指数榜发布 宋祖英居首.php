																											<div id="postmessage_439668" class="t_msgfont">中新社北京1月18日电&nbsp;&nbsp;备受关注的第三届华鼎奖中国演艺名人道德修养指数榜今日在此间发布，其中宋祖英、山西电视台专题主持人费婕、艺人韩庚分列五百艺人、一百主持人、一百新演员道德修养榜单之首。综合三榜，宋祖英道德素养满意度最高，苏州滑稽剧团团长顾芗垫底。<br />
