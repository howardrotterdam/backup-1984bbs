													<h2>南京澄清6月13日将有大地震谣言 拘捕女发帖人</h2>
						
						
																											<div id="postmessage_585527" class="t_msgfont">来源：新华报业网-扬子晚报<br />
<br />
网传南京6月将有大地震，谣言！<br />
<br />
　　本报讯(朱姝) “美国宇航局、美国国家地震台网预测：中国南京大地震将于2010年6月13日降临。震中为江苏省南京市，震级为7―8级左右。”近段时间，网上热传一个名为《南京6月13日将有7―8级大地震》的帖子，引起了很多网友的关注，并扩散到市民社会生活之中，产生了消极影响。<br />
<br />
　　昨天，记者发现，南京市地震局网站上发表了一篇名为《南京近期发生7级地震是谣言》的文章，公开回应：有关我市要发生7级地震的谣言纯属无稽之谈，请广大市民不要相信，更不要传播，以免对本人和他人的生活产生影响。<br />
<br />
　　不光南京 多个地区被传有大地震<br />
<br />
　　“据江苏地质勘测局发布消息：南京江宁区有超过76%的温泉出现水温上升、水色变黑等现象。有关专家已于4月20日上午10时许抵达南京市，经相关勘测仪器探测表明：地下轰鸣声增强，相比以往&quot;提高&quot;了五十五个分贝。预计此次6月13日南京大地震，其相连县市如镇江、常州、无锡、滁州等都会受波及。”记者昨天一打开QQ，就有这样的链接出现。不光记者，周围的好多人都曾经收到过这样的消息。<br />
<br />
　　而在网上输入“6月13日地震”，就立即弹出了200多万个帖子。记者注意到，在这些帖子中，不光有预测南京要大地震的，还有预测佛山、陕西、河南等好几个地方“有地震”。“光从这一点说，也肯定就是谣言。”省地震局副局长张振亚表示。<br />
<br />
　　时间地点太具体肯定是谣言<br />
<br />
　　其实，地震谣言已经“盛行”了很长时间。从今年年初开始，网上不断出现哪里要发生大地震，或是哪天某个时刻要出现几级地震的传言。<br />
<br />
　　“其实目前地震谣言的主要内容也很雷同。”南京市地震局专家说，比如把一些自然现象，如由于气候返暖果树二次开花，春季大地复苏解冻而引起的翻砂、冒水等现象，误认为是地震异常，这也最易成为地震谣言的“依据”。<br />
<br />
　　“其实判断地震谣言很简单。”张振亚说，超过目前预报的实际水平，时间、地点等要素十分“精确”的所谓地震预报意见，如传闻中地震发生的时间、地点非常具体，甚至发震时间精确到“上午”、“晚上”，就是地震谣言。“目前，人类对于地震认识还很少，能预报出哪天在哪发生几级地震是不可能的事。”<br />
<br />
　　据悉，《中华人民共和国防震减灾法》第二十九条已明确规定：国家对地震预报意见实行统一发布制度。全国范围内的地震长期和中期预报意见，由国务院发布；省、自治区、直辖市行政区域内的地震预报意见，由省、自治区、直辖市人民政府按照国务院规定的程序发布。任何单位和个人不得向社会散布地震预报意见及其评审结果。<br />
<br />
　　为什么会产生谣言？<br />
<br />
　　① 把一些自然现象，如由于气候返暖果树二次开花，春季大地复苏解冻而引起的翻砂、冒水等现象，误认为是地震异常。<br />
<br />
　　② 地震部门正常的业务活动，如野外观测、地震考察、对某种前兆异常的落实、防震减灾宣传等，引起猜疑。<br />
<br />
　　③ 来自海外蛊惑人心的宣传，或别有用心的造谣。<br />
<br />
　　④ 受封建迷信思想的蒙蔽而上当受骗。<br />
<br />
　　怎样识别地震谣言？<br />
<br />
　　① 超过目前预报的实际水平，三要素十分“精确”的所谓预报意见。如传闻中发生的时间、地点非常具体，甚至精确到“上午”、“晚上”。<br />
<br />
　　② 跨国地震预报。如果传说地震是由外国人预报的，那肯定是谣传，因为既不符合我国关于发布地震预报的规定，也不符合国际约定。<br />
<br />
　　③ 对地震后果过分渲染的传言。特别是强震发生后常会出现“某个地方将要下陷”“要遭水淹”，这种耸人听闻的消息也是不可信的。<br />
<br />
&nbsp; &nbsp; 发帖谣传地震，拘她<br />
<br />
&nbsp; &nbsp; 本报讯 一名年轻女子在网上发帖散布谣言，称南京近期要地震。昨日，南京浦口警方传唤了发帖者。目前，该女子以扰乱公共秩序被警方行政拘留。<br />
<br />
　　 4月25日晚9时许，南京警方发现，有人在互联网上发帖散布谣言称：南京近期要地震，大家要有所防备。网警部门迅速安排专人调查此事。昨天下午4时许，浦口警方根据线索找到了发帖者丁某。经查，丁某，女，25岁，经营一家时尚摄影中心，平时喜欢上网发帖。25日晚7时许，丁某在一好友的QQ空间内看到了一个标题为“专家预测南京有地震”的帖文，联想到最近看的电影《2012》中的恐怖场面、青海玉树地震和最近南京反常的天气，丁某越看越觉得害怕。<br />
<br />
　　随后，丁某在论坛的“婚后空间”和“浦口论坛”上将该帖文转载，很快就有数十人跟帖。直到有网友指出此帖的许多不实之处时，丁某才意识到此帖文是谣言。经核实，此谣言为抄袭近日网上出现的另外一条陕西地震谣言内容。昨天下午，南京市地震局也在其官方网站发布《南京近期发生7级地震是谣言》，对其帖文进行了辟谣。<br />
<br />
　　昨天晚上9时许，浦口警方根据《治安管理处罚法》第二十五条规定，以散布谣言扰乱公共秩序对丁某行政拘留。警方提醒市民，不要轻信网上谣言，更不要在网上发布没有任何事实根据的帖文。（宁公宣 罗双江）<br />
