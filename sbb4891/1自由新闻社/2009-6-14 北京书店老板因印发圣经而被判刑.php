基督徒及书店老板石维翰<br />
<br />
北京法院这个星期判决基督徒以及书店老板石维翰三年徒刑，罪名是他印发和免费赠送圣经属于“非法经营”。观察人士说，这显示中国的宗教信仰自由，仅仅是宪法上的一纸空文，人民的宗教信仰还是受到极大的限制甚至剥夺。<br />
<br />
38岁的石维翰是北京市一家基督教书店老板，也是一个家庭教会领袖。北京市法院星期三以他印发和免费赠送圣经属于“非法经营罪”，判处3年徒刑并且罚款15万元人民币。其他六名与他共同被起诉的还包括教会同工以及印刷公司人员，也都分别被判刑或罚款。<br />
<br />
*家属无法接受判刑决定*<br />
<br />
石维翰的妻子张晶对美国之音表示，家属对判刑之重有些讶异，石维翰12岁与8岁的两个女儿对这样的结果更是不能接受。张晶说：“两个孩子反正有点不能接受，觉得爸爸好像没有罪的，怎么还被定罪，被判刑。因为他们想的就是好像爸爸终于可以回家了，我们觉得就是没想到这么重。”<br />
<br />
石维翰是在2007年11月28号被当局逮捕，2008年1月4号当局宣布将他无罪释放，但很快地又在3月19号将他逮捕，并送往海淀看守所关押至今。<br />
<br />
张晶表示，石维翰有严重的糖尿病，关押一年多时间里，当局不准家属探视，也不让送药。律师提出的保外就医请求也没有得到任何回音，让他们对石维翰的健康状况非常担心。<br />
<br />
*辩护律师至今未通过年检无法代理*<br />
<br />
虽然北京法院在判刑后允许家属在15天内提出上诉，不过张晶表示，由于辩护律师张星水还没有收到当局发放的年检合格证，就算他们想上诉也没有办法。张晶说：“张律师他们的律师事务所已经没有通过年检，所以律师不能再去和他(石维翰)见面，所以可能也不能够再继续有上诉的动作，就是他们不能再代理我们的案子了。”<br />
<br />
北京市独立政治评论员余杰认为，在中国只有南京的爱德基金会得到官方许可，能够印发圣经，而且也只能在官方认可的教堂内才能买到圣经，其他任何的书店和市场都不能买到。也就是说，圣经在中国只能算是半合法的书籍。余杰指出，中国官方还利用南京这个唯一的圣经印刷厂，不断向西方展示说中国民众有买到圣经的渠道。但实际上，中国政府是不断在对人民的宗教信仰进行打压。<br />
<br />
余杰说：“中国现在的宗教信仰自由，仅仅是写在宪法上的一纸空文，在现实生活中受到很多的限制甚至剥夺。比如说，宗教信仰自由不是一个孤独抽象的自由，它和其他权利紧紧联系在一起，比如说新闻出版的自由。”<br />
<br />
*美国驻华使馆曾致电家属表达关切*<br />
<br />
由于石维翰的小女儿是在美国出生，拥有美国国籍，石维翰的妻子张晶表示，在石维翰被关押起诉后，美国驻华使馆也曾经几次致电家属，表达关切。<br />
<br />
美国之音记者打电话到海淀公安局希望了解石维翰的情况，接听电话的人员要求记者发出书面声明，才愿意接受采访。<br />
<br />
至于中国外交部发言人办公室人员则表示，要去了解案情后才能答复记者询问，不过直到截稿前，记者都没有得到中国外交部的回音</div>

							
							
							
							
