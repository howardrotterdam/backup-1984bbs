													<h2>天津联通封杀高校ADSL共享 破门收走学生设备</h2>
						
						
																											<div id="postmessage_822308" class="t_msgfont">网易科技讯 9月9日消息，中国联通天津分公司近日对天津几所高校的ADSL共享上网进行了“封杀”，联通工作人员进入学生寝室更换了上网设备，从技术上屏蔽了一根网线多人共享的行为。在部分高校，联通趁暑假期间破门进入学生寝室更换设备，并没有提前告知学生。<br />
<br />
<img src="http://img.cnbeta.com/newsimg/100909/17201301866463524.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
<br />
运营商限制ADSL宽带共享的新闻近几年来时常见诸媒体，大多都是通过技术手段加以限制，类似天津高校这样入室收走学生设备的情况还比较少见。<br />
<br />
我的“猫”哪去了<br />
<br />
8月28日，天津某高校大三学生小李过完暑假返校，寝室门口的情景让他紧张起来――他离校时，学校统一在寝室门上贴了一个封条，但现在封条被撕掉了。他担心寝室被盗，急忙打开门，仔细检查后发现，只有自己放在桌子上的用于上宽带的ADSL Modem（俗称“猫”)和自己买的路由器不见了，取而代之的是一个标有“中国网通”“亲情1+”字样的白色设备。<br />
<br />
小李随后知道，凡是将“猫”和路由器放在桌子上的寝室，这两样东西都不见了，而将“猫”和路由器锁在柜子里的，都完好无损。小李从寝室管理人员那里了解到，运营ADSL宽带的中国联通天津分公司在暑假期间，经学校同意后，破门进入学生的寝室，将原来放在外面的上网设备都拿走了，学生们要想拿回自己的设备，需要到设在学校的联通营业厅领取。<br />
<br />
在该校的联通营业厅内，大厅的地板上摆放着几个长条形的纸箱，打开的纸箱里装满了从学生寝室收来的“猫”和路由器等。9月5日，该营业厅一位工作人员表示，学生可以从9月10日起凭有效身份证明到联通营业厅领回自己的设备。6日则有另一位工作人员说，之前从联通租用的“猫”将不会退还，凭押金条可以领回当时交的押金；而如果被收走的设备是学生自己的，需要提供宿舍号等证明，之后便可以领回。<br />
<br />
可以取回自己的东西让同学们稍稍感到安慰，但更加令人郁闷的是，联通更换的新设备从技术上限制了ADSL共享，寝室的电脑需要每台都申请一个ADSL账号才能同时上网，这将大幅增加他们的上网开支。<br />
<br />
联通回应：“根据国家规定”<br />
<br />
针对此事，联通在部分宿舍楼公告栏贴出了一份“重要通知”海报，主题是“天津联通定于暑期对本校的互联网设备进行整体升级改造”，称原因是“进一步满足日益发展的校园互联网通信需求，加快校园信息化建设”，并称“根据国家相关部门的规定，一个账号多人上网的方式已经不允许了”。<br />
<br />
这份“重要通知”并非加盖公章的正式文件，同学们表示看不懂海报中所说的原因是什么意思。而且，并不是所有宿舍楼都张贴了此海报，很多同学在班上互相打听后才知道有海报这回事。<br />
<br />
平日里冷冷清清的联通营业厅开学这几天异常热闹，老生过来索要自己的“猫”和路由器，新生不知道怎样设置才能正常上网。对此联通营业员不停的重复一句话解释，“根据国家的规定，这个已经不允许了，现在一些运营商对家庭宽带都开始限制共享上网了”。至于国家的规定是什么规定，相关部门是哪些部门，营业员一概不予回答。<br />
<br />
另外有其他学校的同学称，并非所有天津高校都采取了假期破门更换设备的办法。有些学校是在开学以后联通派人到寝室更换设备的，但在联通来人之前宿舍不能正常上网。<br />
<br />
9日，天津联通一名负责人在电话中向网易科技表示，根据她的了解，联通在大多数天津高校的设备改装都是在假期进行的。她说，联通为了不耽误学生的时间，都会在学生放假时进行改装。“很多学校都是这样的，我自己也跟着去了几所大学，都是这样的。”她同时表示，如果有学生自己的设备被联通收走，她将向公司内部相关部门询问此事，同时建议学生向学校询问。<br />
<br />
近几年来，运营商封杀ADSL宽带共享的新闻时常见诸媒体，但大多都是在服务器端通过技术手段进行屏蔽，类似天津高校这样直接入室收走设备的情况，还非常少见。（詹平）<br />
<br />
