													<h2>唯色驳斥亚洲周刊记者：新疆事件的导火索，韶关事件，究竟真相如何？</h2>
						
						
																											<div id="postmessage_157208" class="t_msgfont">唯色驳斥亚洲周刊记者：<br />
新疆事件的导火索，韶关事件，究竟真相如何？<br />
――致亚洲周刊记者<br />
<br />
&nbsp; &nbsp; 亚洲周刊记者邱立本撰文批评国际媒体有意或无意地扭曲真相，“让事实不清不楚，也让‘七五’事件成为被误读的悲剧。”（见《新疆乌鲁木齐暴乱：真相是受害者》一文）同样作为媒体记者，邱先生又是如何追究真相的呢？<br />
&nbsp; &nbsp; 乌鲁木齐“七五”事件，已经众所周知，其导火索就是6月25日夜至26日晨发生在广东韶关旭日玩具厂的血案。这也是当局所不愿承认又不得不承认的事实，尽管当局一股脑儿地把“七五”事件的罪责归因于热比娅，说是“有组织有预谋”，这委实太陈词滥调了。有网友讽刺说，有德国人的地方就有啤酒，有韩国人的地方就有泡菜，有中国人的地方就有组织有预谋。然而，韶关事件毕竟是绕不过去的一个坎儿。虽然早就是积怨已久，积重难返，用毛泽东的话来说，是一个从量变到质变的过程，而引爆“七五”事件的导火索就是韶关事件。<br />
&nbsp; &nbsp; 那么，韶关事件是怎么回事？非常严肃地追究“七五”真相的亚洲周刊记者，用轻描淡写的一句话做了概括：“‘七五’事件的根源之一，就是周前的广东韶关的两名维族工人死亡事件……”<br />
&nbsp; &nbsp; 好吧。先说维族工人的死亡事件，是怎么致死的？工伤？病故？还是吵架斗殴？其次，维族工人有男有女，而且已经知道，在韶关旭日玩具厂打工的维族工人，女工多于男工，而这公布的已经死亡的两名维族工人，究竟是男还是女？何以不能透露死者的性别等信息？<br />
&nbsp; &nbsp; 更重要的，更需要追究的，其实是在“‘七五’事件……周前的广东韶关”，究竟被打死了多少人？所谓“两名维族工人死亡”，早在6月27日，当局正式公布的消息中就是如此宣布的。也即是说，这正是当局给的结论，那么这就意味着是真相吗？亚洲周刊记者的这句话说得没错，由于韶关事件“并没有在媒体上充分报道，真相和责任不明，种下了恶果”，但他，以及诸如明报等媒体（明报至今的说法还是“汉维两族工人械斗、两名维族工人死亡”），皆都援引当局的这一结论，实际上所起到的效果，也正是后来当局所说的，韶关事件只是一起“普通社会治安案件，被境内外分裂主义势力肆意渲染、炒作成所谓‘大汉族主义’骚乱事件”。<br />
&nbsp; &nbsp; 当时，韶关事件发生之后，充斥中国网络、报纸等媒体的全是很快又被当局否认的谣言，即所谓维族工人强奸汉族女工之说，海外媒体如星岛日报更是率先报道、渲染“强奸说”的，影响极其恶劣。凡是在那期间关注此事件的人都应该知道，由于诸多媒体不负责任地以讹传讹，在网络上激起了多么强烈的、令人怵目惊心的对维吾尔人乃至对整个民族的诋毁、污蔑和赤裸裸的威胁。而在当局出面，说所谓强奸是谣言并拘捕了造谣者（“七五”事件发生之后，新疆主席努尔&#8226;白克力又说在韶关虽未发生强奸，但发生了“挑逗戏弄”），正如一位维吾尔知识分子指出，“我们也发现媒体管理很严的这个国家没有一家媒体为自己传播造谣而受到处罚更有甚至没有一家媒体对此事件进行反思。”<br />
&nbsp; &nbsp; 无论强奸之说是不是谣言，在韶关这家工厂激起的动荡远比在网络上激起的反应要强烈无数倍，甚至无法相提并论，因为在韶关付出的代价是人的生命，实质是严重的族群冲突。更为可悲的是，数日后，在4000公里之外的乌鲁木齐，付出的代价是更多人的生命，维族的、汉族的，活生生的生命；以及极为严重的族群仇杀，族群分裂。<br />
&nbsp; &nbsp; 所有不可能亲临现场也不可能了解详情的人，起先知道的也只能是“两名维族工人死亡”，但是很快，尽管当局对网络上相关信息删除极快，快到几分钟前，在一些个人空间或留言中，还可以看到当时图片和视频，但很快就荡然无存。直到在Youtube上出现几段历时约三四分钟的视频，真相才开始显露。<br />
&nbsp; &nbsp; 这几段视频，既不是热比娅为首的世维会所拍摄的，也不是中国官方派人来拍摄的，并且，也不是在韶关打工的800多维吾尔工人中的谁谁拍摄的。这几段视频，都是韶关工厂的汉族人或在楼上或在地面拍摄的，带有幸灾乐祸的情绪，有人边拍还边说：“可惜用手机拍，镜头拉不过来。”许多人都从Youtube上看见了这几段视频，之血腥，之暴烈，与亚洲周刊记者描述的“七五事件”完全是一样的，同样是“在这冷血之夜被……暴徒杀害，……死状之惨，触目惊心”，而死者是什么样的人呢？难道不也是“与政治无关”的普通人吗？他们被新疆政府组织到内地打工之前，是在乡下种地收棉花卖哈密瓜的年轻农民（其中多数是来自农村的未婚女性），而他（她）们从新疆老家抵达广东工厂才一个多月。<br />
&nbsp; &nbsp; 我也看了Youtube上的视频，我不愿意再重复描述视频中所展示的惨状，只在这里提供一个当时在现场参与围殴维吾尔工人的汉族工人的记录：“据转有数名新疆人不治！现场一片狼棘，半人型大的血泊有数十处……洒落一地的钢筋铁棒约二百多支，灭火筒一百多个全被打的弯曲凹凸……”第二天，“旭日厂宿舍区一百多名搞卫生的工人整整用了两个小时才把遍地的血迹冲洗净……”<br />
&nbsp; &nbsp; 除了视频（现在，Youtube上的有的视频已被删除），之后还有一些同样血腥、惨烈的图片，陆续出现在网上。亚洲周刊的记者先生，如果您说，一大群人举着钢筋铁棒等凶器围追着打一个一个的人，以至于打得一动不动了还在打，甚至警察就在跟前，可还在往死里打，甚至地上已经堆叠着数个血肉模糊的人，而这些人都没事，都没死，都不是那确定死亡的“两名维族工人”，如果您坚持要这么说，当然，您完全可以这么说，因为您也可以认为这根本就不是什么真相，那么，我也就毫无必要在这里喋喋不休了。<br />
&nbsp; &nbsp; 其实，我赞同您提出的问题，“中国怎样在全球的舆论战场中重建公信力？‘七五’事件中，当局为什么不主动发放遇害者的姓名和背景？为什么不让记者到每一家医院去调查死难者的情况？为什么不让死难的汉人的家属来说话？”因为，同样的问题也可以是关于之前的韶关事件，毕竟，韶关事件既然是“七五”事件的导火索，究竟是“两名维族工人死亡”，还是传言有十八名甚至更多名维族工人死亡，若不追究，若不搞清楚（这难道会很难吗？发生在一个工厂的暴力事件难道比发生在一个大城市的暴力事件更难了解吗？而人的生命，绝不仅仅只是几个微不足道的统计数字），同样是您所批评的，“无意与有意之间，就很容易以讹传讹，以假代真”，以至于事态不断恶化成不可挽回的危局。作为知名媒体的资深记者的您说是不是这样？<br />
&nbsp; &nbsp; 请不要选择性失明，正如我的一位网友就韶关事件的评述：“假如那些爱自由的人们不能做到不分贫富、地域、种族的平等，那么，他们将永远无法摆脱威权分而治之的思想桎梏，他们将永远成为稳定策略下的一颗颗无辜也无知的棋子。”<br />
<br />
2009-7-10，北京</div>

							
							
							
							
