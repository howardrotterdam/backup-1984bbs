杨刚，J中校，张洁平：新疆百余汉人被杀西方媒体误读悲剧 <br />
新疆首府乌鲁木齐七月五日傍晚出现恐怖袭击，一些有预谋的维族暴徒以利刃，钝器或汽油弹突袭街头汉族平民，并焚烧汉族，回族等其他种族的商店，车辆，导致百余汉人死亡，但一些西方媒体却称喋血事件是另一个“天安门事件” ，暗指受害者是维族人。中国官方对内报道含糊其辞，避免全国汉族向身边维族报复，也使真相不明，外媒误读悲剧。 <br />
<br />
-------------------------------------------------- -------------------------------------------------- ------------------ <br />
<br />
乌鲁木齐的交通终于恢复，武警控制了全城局势，但汉人仍心有余悸。七月八日下午友谊医院，仍有伤者昏迷不醒。医生说事件发生后抢救不治的死者都是汉族， “伤者也大多数是汉族，也有其他民族，包括维族，不过很少。 “汉人死者中有的头部几乎被砍下来，极为恐怖。 <br />
<br />
七月五日晚上八点中国内陆新疆首府乌鲁木齐爆发近几十年来最严重的骚乱，暴徒针对汉人进行打，砸，抢，烧，气氛恐怖惨烈。伤亡数字在不断上升，官方公布，截止七月六日二十二时，已造成一百五十六人死，一千零八十人伤，二百六十余辆机动车被烧（警车十一辆） ，二百零九家店铺，门面及两座楼房被焚烧。 <br />
<br />
不过，也有内部消息称，死亡人数实际在三百八十人以上。现场目击者表示，死者大多数是汉族人。这场死伤惨烈的突袭，导致七日上万汉人持械走上街头，要求打击恐怖分子。 <br />
<br />
对于这场以种族歧视名义进行的恐怖袭击，当局显然应对不足。当时北京官方未公布死伤者详情，更没有现场图片，只有少量视频，中国多数民众对遥远的新疆发生的事情，不甚清楚。 <br />
<br />
但经常上网的网民却在饭否，叽叽喳喳上不断得到现场消息，而且发现不少国际媒体的报道竟与事实真相南辕北辙，比如错误使用石首事件的图片，在真相尚未明晰的情况下就习惯性定性为中共武力镇压造成伤亡。亚洲周刊在现场调查发现，情况并非如此。 <br />
<br />
当地人回忆，七月五日下午，乌鲁木齐市维吾尔族姑娘纷纷戴上了头巾。在当地的习俗中，这意味着将有重大事情发生。当晚，六点二十许，有两百多人在人民广场聚集，被劝离，这些人当中有不少维吾尔族学生。现场目击者说，当时维族游行队伍四周均有维族人控制，不与邻近汉人发生冲突。赶到现场的警力也只是封路，并未干涉游行。 <br />
<br />
后来，七十多名挑头闹事分子被公安机关带走，其他游行者被驱散。被驱散的维族人向解放南路二道桥，山西巷片区聚集，高喊口号，现场秩序混乱。在当地维族学生最集中的高校-新疆大学，维族学生们喊着口号，在小卖部集合，然后冲上街头。在之后的骚乱中，暴徒们点燃了新疆大学门口的两辆汽车。到十九点三十分时，上千名维族人在山西巷妇幼保健医院门前聚集;四十分，在人民路，南门一带又有三百多人堵路。 <br />
<br />
打砸抢是在晚上八点十八分左右开始的，暴力犯罪分子推翻街道护栏，砸碎三辆公交汽车玻璃。八点三十分许，暴力升级，暴徒们开始在解放南路，龙泉街一带焚烧警车，殴打过路行人，约有七，八百人冲向人民广场人民解放军进军新疆纪念碑，沿广场向大小西门一带有组织游蹿，沿路打砸抢烧杀。 <br />
<br />
在北湾街狭长的巷道里，暴徒先是袭击了横过来的小车，然后将小车掀翻横挡在马路当中，堵住狭长的巷子，后面的十一辆车既来不及躲闪，也无法调头，全被烧毁，只剩下车架子。暴徒点燃了黑甲山前街一家粮油店，店主一家四口全被活活烧死;在二道弯路盲人学校附近，上百居民拿起棍棒，与已经疯狂的维族暴徒对峙，保护家园。 <br />
<br />
这是一起直接针对汉族人的暴力犯罪事件，在这里，除了维族人开的店面，余下的都被砸毁。疯狂的暴徒甚至也波及其他民族，一些回族商店被砸。一名维族的士司机说，他将车子停在山西巷附近，也被砸了。 <br />
<br />
到了晚上九点多，约有两百余名维族青年在自治区党委东门附近高喊口号，企图进入未遂。政府调集了上万名警力进入广场，南门，团结路，赛马场，新华南路，新疆大学，红雁池电厂等事态严重的地点进行处置。但此时，暴力在警力照顾不到的地方开始进行。暴徒们分多路，多股行动，制造血腥事件。不少行人在背街小巷遭受袭击，无路可逃。市民说， “维族暴徒把汉人拉到巷弄里杀害。 ”官方公布死亡数字时指出，至少有五十七具尸体是从背街巷道中发现的。 <br />
<br />
韶关事件谣言疯传 <br />
<br />
预谋暴力的一些迹象在几天前就已经展露出来。广东韶关械斗事件传出后，六月二十九日，两名新疆籍务工人员的遗体空运到家乡安葬，而在新疆，却流传了另外一个版本。 <br />
<br />
乌鲁木齐市的士司机小洛说，当地的版本是，韶关工厂里的汉族人诱奸了维族姑娘，几个维族小伙不服气，前去理论被群殴致死。谣言在维吾尔族人中间流传开来。直到七月七日，上街示威的维族妇女中还有人相信，在韶关她们有四百个姐妹被强奸。 <br />
<br />
世界维吾尔族代表大会发言人迪里夏提也对记者表示，韶关事件“反映出对于维吾尔人的仇视是中共煽动的。 ”世界维吾尔代表大会（简称“世维大会” ）是二零零四年四月在德国慕尼黑成立的，为东土耳其斯坦运动相关组织的统一领导机构，现任主席为热比娅卡德尔。其前身是两个组织，一个叫做“世界维吾尔青年代表大会” ，一个是“东突民族代表大会“ 。二零零三年的十二月份，世界维吾尔青年代表大会被中国公安部认定为一个东突的恐怖组织。 <br />
<br />
新疆的“维吾尔在线国际”等网站不断有帖子煽动称，韶关“六二六”事件，是中国政府有组织，有预谋的种族清洗。 “维吾尔在线国际”中文网到现在还可以看到类似的谣言。该网站有网友表示，他们作为内部会员，愿意出来举证。 <br />
<br />
根据当地政府掌握的消息显示：七月四日晚开始，一些“别有用心”的人在联系群，论坛，个人空间大量发帖，呼吁鼓动维吾尔族群众七月五日十七时在乌鲁木齐市人民广场，南门举行示威游行，并且还有人大量发送信息响应境外“三股势力” （分裂主义，极端主义与恐怖主义）在各国举行的示威游行。 <br />
<br />
而在国外，世维大会主席，流亡美国的热比娅公开发言，七月五日乌鲁木齐会发生一起大的事件，要求境内联系人关注和搜集事件的相关情况。相关部门的情报显示，七月五日当天，热比娅还同国内通电话进行煽动，称“要勇敢一点” ， “事情闹大一点。 ”热比娅接受半岛电视台采访时承认号召海外维族人上街抗议韶关事件。 <br />
<br />
新疆自治区政府表示，七月五日发生在乌鲁木齐市部分地区的打砸抢烧杀严重暴力犯罪事实，是一起在境外“世维会”等“三股势力”组织直接煽动，策划下实施的有组织有预谋的犯罪事件。 <br />
<br />
对政府的诘问也在流传。一名官员认为： “既然政府早已经掌握了信息，为什么没有及时控制？ ” <br />
<br />
暴乱前运砖进城 <br />
<br />
种种迹象表明，无论是挑头者还是暴徒行动方案，都是训练好之后，精心准备而来。 “在乌鲁木齐市区，很少找得到砖头，暴徒当天用来打砸的砖头是用卡车一车一车拉过来的“ ，一名受伤的妇女说： ”这样明显的行动，政府事先居然不知道？ “ <br />
<br />
显然，这种利用“和平示威”作为掩护的恐怖袭击，让当局措手不及，警力分散，结果防不胜防，导致暴徒为所欲为，死伤惨重。 <br />
<br />
长期关注边疆问题的北京学者蒋兆勇认为，有人正是借韶关事件试图加深维汉矛盾“ ，从偶发的个别治安械斗事件中抽象出民族压迫话语，并以此作为动员框架，有组织地制造暴动“ ， ”暴动的一个重要目的是激活维汉关系的边界，制造维汉『敌我』 “ 。 <br />
<br />
另外，中共建政后，中央政府利用行政区划上的制衡，设立新疆兵团，曾有效地维持了新疆的稳定。近年来由于地方党政系统长期经营，形成特殊利益集团，不惜欺下瞒上，把民族问题动辄归咎“分裂势力” ，挟持中央政策，同时对少数民族干部不信任，使得新疆民族矛盾不断激化。 <br />
<br />
新疆当局为控制舆论和信息，乌鲁木齐市内所有网络信号被屏蔽，手机信号也时断时续，拨打当地电话会被告知，这是空号或者号码有误。乌鲁木齐市委书记栗智说： “有消息表明，境外分裂势力与国内分裂分子通过互联网沟通。为了切断他们的联系，而采取了网络管制，直到事态进一步稳定，平息“ 。七月七日官方宣布，当地警方已逮捕一千四百三十四人，其中男性一千三百七十九人，女性五十五人。 <br />
<br />
事发后新疆官方组织境外媒体和境内指定的个别媒体采访，其他境内媒体均收到禁令。当境外媒体七日外出采访时，遭遇数百维族妇女抗议当局抓人打人。境外媒体全天连续不断播发维族妇女哭诉的新闻。乌鲁木齐汉族市民怒道，他们要被抓的丈夫，那些死者的家属又找谁去要人？当天又传出有维族人闯入汉族人居住区砍人。乌鲁木齐的汉族居民怒不可遏的走上了街头。 <br />
<br />
七五事件发生后，七月六日早上就有各种镇压消息和图片在网上传开，包括借用石首事件的图片等。更有人主动向国际媒体爆料，述说中共武警残暴镇压。世维发言人迪里夏提更对媒体说， “新疆事件解放军拿冲锋枪扫射。 ”得到这些消息后，不少国际媒体在没有现场证实情况下作出单方面报道，更错误引用刊发伪造图片，在中国网民引发强烈抗议，这是去年西藏事件后外媒公信力在中国再次遭遇危机。 <br />
<br />
美国较早的一篇“中国新疆发生动乱”的报道中，通篇只引用了对一名维吾尔族目击者和一名世维会发言人的采访，认为周日下午的警民冲突是由维族人的和平游行演变而来的，让人以为受害者多为维族人。这篇报道受到了中国众多网友的抨击。而动乱发生的第二天，自由亚洲电台（ RFA ）的，路透社也将一张上月湖北石首事件的照片误当新疆暴乱的图片发出来，被英国“每日电讯” ，香港及台湾等一些媒体转载引用。更荒谬的是，这幅“张冠李戴”的图片也被世界维吾尔代表大会主席热比娅在七日接受半岛电视台采访时亮出，作为中共解放军进城镇压的证据。 <br />
<br />
台湾支持台独的“自由时报”标题出现“中国血腥镇压” ，通篇报道只引用了世界维吾尔大会的数据和消息，并且在六日就发出乌鲁木齐市有“坦克辗毙学生”的报道。而七月六日开始在网上疯传被认为是维族学生倒地的一张图片，后来被中国网友发现其实是零八年八月上海人民广场的一张图片。 <br />
<br />
最新的消息是，八日下午从深圳等地抽调来的五千特警刚下飞机。目前当地所有武装力量均为武警或警察，并未见到解放军进城。在友谊医院，亚洲周刊记者发现死伤者身体上都是刀伤，砍伤，钝伤等外伤，没发现枪伤。英国“卫报”记者马尔科姆摩尔七月七日在叽叽喳喳上发的消息也称“极少枪伤” （很少有子弹伤口） 。 <br />
<br />
面对国际媒体错误不断的报道，北京当局七日下午发布了部分“七五事件”现场图片。图片之血腥让人震惊。有意思的是，这些图片都只是对境外媒体发放，境内媒体和网站没能刊出这些图片。饭否网友认为，北京官方对于死伤具体数字的暧昧和对受害者民族身份的有意模糊，不仅仅是出于习惯性的新闻控制，更主要是怕各地的汉族民众得知残忍的真相后，对身边的维族进行报复性攻击。北京当局用心良苦，但似乎反而使国际媒体出现更多失实报道。 <br />
<br />
西方媒体涉嫌误导读者 <br />
<br />
其他的媒体就更技巧的利用这种语言模糊的歧义，甚至加上血腥镇压，暴力镇压的字眼。因此不少读者以为这次又是一个“天安门事件” 。七月七日英国广播公司的记者霍格说克里斯，这次新疆动乱是和天安门事件等同的暴力事件，不过他这句话后来被替换成了“这是中国几十年来最严重的暴力事件” 。不少国际媒体和人权组织后来只关注中国当局抓了多少维族人，而不深究残暴屠杀的受害者，被人质疑他们的公正性。 <br />
<br />
北京当局的欲言又止，国际媒体有意无意的偏向性报道，让上千无辜死伤者冤魂难安。新疆乌鲁木齐的种族仇杀事件已经进入第四天，尽管当局已基本控制局势，但是恐怖紧张的气氛仍笼罩全疆。 <br />
<br />
七月七日，乌鲁木齐市区。呼啸声从门外传来，一浪高过一浪，位于乌鲁木齐市阿拉泰路的酒店，距离机场八公里，旅客神情慌张，陷入恐慌当中。通往机场的道路已被围堵，进入市区的交通也开始管制，各种不利的消息接踵而至，这里仿佛是一座孤岛。 <br />
<br />
酒店女经理站在门口劝阻欲前往机场的旅客。她说： “你们哪里也不用去，到处都危险，他们要是敢来，酒店有高压水龙头，我们准备了棍棒，守住这扇门，你们在里面是最安全的。 “酒店后面是一家大型汽车修理厂，十多名工人手持铁棍冲出来，站在路边，与马路对面大约两公里的围堵现场遥遥相望，这些年轻的工人们表情愤慨： “我们不是好欺负的，维族要是敢过来，看打不死他们。 ” <br />
<br />
沿街店面的汉人纷纷走出家门，拿着木棍，铁柏，榔头和一切可以自卫的器具，涌向街头。在新疆，除了体貌特征区分了维吾尔族与汉族之间关系，来自“七五”事件的鲜血也为两个民族埋下了隔阂种子。 “要为一百五十多名无辜的人报仇，更重要的是我们要保护自己” ，一名在新疆出生，长大的汉族男子说， “长这么大，还是头一次看到汉人这么齐心。以往，维族人打汉人时，大家都在一旁走过，从现在起不一样了。 “ <br />
<br />
但整个下午，他们没有看到几个维族人，由于找不到目标，没有情绪宣泄的对象，聚集在街头的汉人，在天黑后，渐渐散去。 “维族事先得到了消息，都躲起来了“ ，这名男子说。 <br />
<br />
虽然政府表示已经控制好了局面，却没有安抚好担惊受怕的民心。对维族暴徒的仇恨情绪越酿越深，以致当天中午，乌鲁木齐市内绝大部分汉人走上街头，要求维族血债血偿，民意汇成强大的河流。在七月五日事发的中心区域人民广场，南门一带，汉族群众游行至此，手握棍棒，高喊“反对暴力”口号，打出了标语，政府不得不出动大批武警维持秩序。不过，在部分国际媒体的报道中，这些汉族示威者被称为“暴徒” （暴徒） ，而对维族人一直使用中性的“示威者”等词。美联社当时发出的报道称： “周二，在中国新疆首府乌鲁木齐街上，汉族暴民（暴徒的汉人）挥舞着菜刀和棍棒，而成群的穆斯林维族人（集团Muslism维吾尔族男子）则打人。 ”这种明显差别对待汉族人和维族人的做法，被中国网友批评为“颠倒黑白” 。在乌鲁木齐的外国记者发现，示威汉族人对西方记者不信任，而维族人则对汉族面孔的记者有敌意。 <br />
<br />
在当天的新闻发布会中，新疆自治区党委书记王乐泉并不赞同这种做法，他批评有些单位的汉人情绪激动，将会给分裂分子带来口实。 “我认为这样完全没必要” ，他说： “敌人的目的就是要破坏民族团结挑起民族对立，我们要擦亮眼睛，不要上当;我们要同仇敌忾，把矛头对向境外的敌对势力而不是自己的民族兄弟。 ” <br />
<br />
“并非所有维族都是坏人” ，一名游行的汉人说： “烧杀打砸抢当晚，有些维族在施暴，有些也帮着救人。 ” <br />
<br />
“问题在于，你不知道哪些是好是坏？ ”通过视频可以看到，一些维族学生，妇女甚至小孩都参与示威。 <br />
<br />
在政府抓获的一千多名犯罪嫌疑人当中，有一部分的身份被确认为学生。王乐泉说： “这些年轻人绝大部分都是不明真相被煽动上当的，只要他们没有参与严重的打砸抢行动，很快移交所在单位接受调查。 “同时也强调，将不放过任何一个暴徒。 <br />
<br />
七日下午，政府担心事态进一步恶化，宣布从当晚九点到八日早上八点，乌鲁木齐市内全面交通管制。 <br />
<br />
到了七月八日，中国当局从各地调来特警，在街头获得汉族百姓鼓掌欢迎。他们指出： “早就该来了，不来就对不起我们” ，指出七月五日那天百余汉人被杀害的悲剧，都是由于当时当局反应太慢，没有足够的治安力量。一名汉人居民说： “不能让我们都要带刀子，棍子上街来保护自己。 ”他们指出人民的子弟兵，要在关键时刻来保护老百姓的生命，不能让一些恐怖分子无法无天。 <br />
<br />
乌鲁木齐一片寂静，商业活动基本上停止。你可以看到，绝大部分店面选择了关门，不论是在事发区域还是市中心汉人居住地带。市区内大型商场，比如百盛，新世界等都在下午六点多便开始停止营业。根据新疆的时差，通常是在晚上九点多，夜幕才开始降临。往常这时，夜生活才刚刚开始，热闹正要启幕。 <br />
<br />
而此刻，站在路边的人都在焦急的等待公交车或者的士的到来，在天黑前回到家，几乎是所有人的愿望。即便如此小的心愿都难以达到，因为在夜间，的士司机也停止了营运。随着夜色降临，路面上车辆越来越少，车速越来越快。 <br />
<br />
事发区域内，市区环卫工人清理了所有的残骸，焚烧的汽车已被拖走，倒塌的路标又重新扶起。如果不是街头的武警显得有些突兀，这里仍然相安无事。 <br />
<br />
在二道桥一带，维族人仍像往常一样坐在路边，或者在街头游荡。这是他们的聚居地，平静得仿佛什么也没有发生。但路面仍留有过火的印记以及散落的玻璃残渣，位于跑马场路段的吉利专卖店，店面完全被烧毁，门前停靠的三十三辆汽车都被暴徒撬开车头，放火焚烧，然后推翻在地。 <br />
<br />
这些记忆让汉族人颇受伤害，一名中年男子说，这两天，他一直没开车出去，在家里憋着，越憋越难受。他说，现在开车到街上，见到维族人就有种想撞过去的冲动。 <br />
<br />
内心隔阂始终存在 <br />
<br />
在乌鲁木齐这座民族大融合城市，表面上看，各民族间相安无事。街头一派繁华，但内心隔阂始终存在。 “最可怕的是，这些人是你的邻居，同事，突然有一天就变成了凶残的敌人。 “这名男子说。 <br />
<br />
目前，公安机关已抓获一千多名犯罪嫌疑人，但这个数字是动态的。滞留人员目前尚在一一审理甄别中。 <br />
<br />
七日，无论是维族人还是汉人示威游行态势都被政府控制住，约一个师的武警在市区内巡逻防卫。但众多商铺仍未正常营业，汉族市民情绪仍在宣泄中。许多人打电话求助电台，一名男子打电话诉说了自己的苦闷，他说： “我想问的是，我们能做什么？ ”<br />
