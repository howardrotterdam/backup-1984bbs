　　中级士官吴佳中国军鸽指挥“第一兵”<br />
<br />
　　初夏的云贵高原，天高云淡，成都军区某部一场演习激战正酣。<br />
<br />
　　狭长的山谷里，“红军”一部被围堵。在“蓝军”强大的电磁干扰下，“红军”通信信号中断，战场情况十分危急。 <br />
<br />
　　“啪啪！”一只黑褐色军鸽拍打着翅膀，带着求救信息冲出山谷，直奔“红军”后方指挥部。<br />
<br />
　　不多时，“红军”援兵悄然而至，从“蓝军”背后猛烈一击，战场形势峰回路转……<br />
<br />
　　这只突出重围的军鸽“森林黑”，正是出自成都军区某通信器材仓库军鸽队驯养师、中级士官吴佳之手。<br />
<br />
　　作为全军唯一军鸽队的驯养师，吴佳可是“腿上绑铜锣――走到哪响到哪”。军鸽队长陈春桃说，吴佳携军鸽征战收放自如，能抗老鹰袭击、越高辐射区、抗强电磁干扰，屡屡建功。入伍10年来，经他驯养的军鸽参与各类演练、演习及救援任务达百余次，排除复杂险情百余起，堪称中国军鸽指挥“第一兵”。<br />
<br />
　　让军鸽听话的绝活儿从哪里来？走进吴佳的宿舍、工作间，你就能有所感悟。10年的军鸽驯养生涯中，吴佳“啃”下了30多本专业书籍，记录下15万字的笔记，熟记了数百组技术参数、数百张蜘蛛网般的训练分析图。<br />
<br />
