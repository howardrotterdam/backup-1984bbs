<br />
　　能否使经过防鹰训练的军鸽这种“后天获得性”变成比较稳定的生物学特性呢？经过复杂的多代育种、选择和训练，他和军鸽队的队友们终于培育出不怕鹰的军鸽――“森林黑”。这是吴佳入伍10年来最引以为自豪的“杰作”。(游罡　特约记者　梁洪清)<br />
