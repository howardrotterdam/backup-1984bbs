													<h2>共军称西方军队国家化军事制度行不通</h2>
						
						
																											<div id="postmessage_710032" class="t_msgfont"><a href="http://mil.news.sina.com.cn/2010-06-29/0635598390.html" target="_blank">http://mil.news.sina.com.cn/2010-06-29/0635598390.html</a><br />
<br />
　　刘新如&nbsp;&nbsp;齐&nbsp;&nbsp;彪<br />
<br />
　　编者按<br />
<br />
　　去年12月以来，本刊在思想理论界率先发表划清“四个重大界限”的系列文章，引起军内外读者关注。按照党的十七届四中全会《决定》要求，划清马克思主义同反马克思主义，社会主义公有制为主体、多种所有制经济共同发展的基本经济制度同私有化和单一公有制，中国特色社会主义民主同西方资本主义民主，社会主义思想文化同封建主义、资本主义腐朽思想文化的界限，是加强党的意识形态工作和思想政治工作、推动社会主义核心价值体系建设的重大步骤。结合部队实际和意识形态领域斗争实际，“七一”前夕我们又组织了这篇专稿。自觉划清中国特色基本军事制度与西方军事制度的界限，有助于深入把握社会主义核心价值体系，进一步打牢高举旗帜、听党指挥、履行使命的思想基础。<br />
<br />
　　“金星闪耀在军旗上，我们的原则是党指挥枪”。新中国成立前夕，在重新设计我军军旗时，毛泽东同志明确提出，军旗上要有五角星，象征党对军队的绝对领导。这一设计其实是一个新兴执政党带有鲜明象征意义的制度奠基。<br />
<br />
　　胡锦涛主席最近强调指出：“党对军队的绝对领导，是我军建军的根本原则和永远不变的军魂，是我国的基本军事制度和中国特色社会主义政治制度的重要组成部分，是党和国家的重要政治优势。”这一重要论述深刻总结历史、洞察现实，精辟揭示了党对军队绝对领导在我国军事、政治制度体系和军队建设中的重要作用和突出地位。从制度层面深刻认识“军队非党化、非政治化”和“军队国家化”的本质和危害，自觉划清中国特色基本军事制度与西方军事制度的界限，对于抵御西方敌对势力鼓噪的错误思潮的侵蚀，进一步打牢坚持党对军队绝对领导的思想基础，在履行新的历史使命中永葆我军本色，无疑有着重要的理论意义和实践价值。<br />
<br />
　　<strong>世界上没有军事制度的“通则”――一个国家采取什么样的军事制度是由国情军情决定的</strong><br />
<br />
　　军事制度的基本问题是军队由谁领导和怎样领导的问题。《孙子兵法》的“兵者，国之大事也，死生之地，存亡之道，不可不察也”的醒目开篇，宋太祖赵匡胤的“兵权之所在，则随之以兴；兵权之所去，则随之以亡”的执政箴言，都深刻阐明了军事制度的极端重要性。<br />
<br />
　　经济基础决定上层建筑。作为一种国家政治制度，军事制度是建立在一定经济基础之上并为其服务的上层建筑。历史经验表明，一个国家采取什么样的军事制度，主要取决于这个国家的历史文化传统、现实国情军情和经济社会发展水平等因素，世界上没有普世的、通用的军事制度。<br />
<br />
　　随着人类社会的发展和政治形式的演变，军事领导制度经历了由君主政治下君权至上制度向政党政治下政党掌握军队制度的演变。在不同的政治制度和政党制度下，军事领导制度表现出不同形式。在西方资本主义制度及两党或多党体系下，政党不拥有自己特定的武装力量，而主要是通过政府这个中介环节来掌控军队。随着政党的轮替，政权的交接，军队的控制权也随之转移。所谓的“军队国家化”，实际上就是两党或多党制下政党通过政府轮流控制军队的方式。而在社会主义制度及一党领导下的多党合作和政治协商制度体系下，实行政党直接领导军队的制度。这种制度由俄国的布尔什维克党首先创立，我国实行党对军队的绝对领导是这种制度更高、更完善的体现。<br />
<br />
　　任何制度都是具体的、历史的。中国特色基本军事制度与西方军事制度的差别性，归根到底是由其国情军情的差别性决定的。各个国家实际情况不同，其制度必定迥然有异。从军队产生的历史来看，西方国家军队通常不是由政党组建，一般来说，军队产生在前，政党出现在后。比如，英国资产阶级的职业军队创建于1642年，而其政党产生于1679年以后；美国军队创建于1775年独立战争中，其政党则产生于1787年以后。而包括我国在内的不少国家是先组成政党，后由政党组织自己的军队，政党与军队合成一体，天然地形成直接关系。从政党产生的历史来看，西方国家政党不是在革命环境中产生，而是在建国后由议会中不同派别逐步发展起来的。包括我国在内的不少国家的政党往往是在民族危亡及武装斗争条件下应运而生，政党不是产生在公开的议会，而是产生在秘密、非法的条件下；不是产生在开国后的和平年代，而是产生在战斗频仍的革命时期。政党要在政治上有所作为，必须建立自己直接掌握的军队。在革命胜利后还要保卫胜利成果，完成执政使命，因而必须直接领导和指挥军队。<br />
<br />
　　一个国家的军事制度，也是由经济社会发展水平和政治制度决定的。在我们这样一个多民族的发展中大国实现现代化，首要条件是必须有坚强的政治领导和长期稳定的政治环境。这就要求有一个坚强的政党作为国家民族的中流砥柱和政治核心，把十几亿人的意志和力量凝聚起来，向着社会主义现代化目标迈进。坚持中国共产党的领导，是历史和人民的选择，也是我国社会制度的根本原则和鲜明特色。政治制度决定军事制度。与中国特色的政治制度相适应，在对军队的领导权上，只能由中国共产党来领导，而不能由其他任何政治力量来领导。这一制度与西方国家根本不同，与前苏联等国家也有明显区别。坚持党对军队实行绝对领导，是我们党的核心领导地位在军事领域里的必然体现，也是中国特色社会主义政治制度不可分割的重要组成部分。<br />
<br />
　　任何产生在特定国情军情条件下的制度，其适用范围也只能是一些特定国家，决不能无限制地扩大，当作“通则”推之于全世界，强加于历史、现实条件都相去甚远的国家和地区。20世纪80年代末90年代初以来，以美国为首的西方国家以多种方式向世界推行包括多党制、“军队国家化”在内的制度模式，对一些国家和地区进行旷日持久的“民主化”改造，由此造成了严重后果，苏联东欧、非洲大陆、阿拉伯世界等地区成为受害的重灾区。多党制和“军队国家化”实际上已经成为西方搞垮社会主义国家、搞倒所谓“集权”国家、搞乱“不听话”国家，进一步称霸全球的重要战略工具。这一教训惨痛而深刻，这种悲剧绝不允许在我们国家重演。<br />
<br />
　　<strong>党和国家的重要政治优势所在――西方“军队国家化”的军事制度在中国行不通</strong><br />
<br />
　　中国近代以来的历史证明，西方的政治制度在中国行不通，同样，由政治制度决定的西方军事制度，在中国也行不通。从一定意义上说，党对军队的绝对领导之所以得以产生，就是照搬西方军事领导制度遭到失败后的必然选择。<br />
<br />
　　近代以来，中国曾先后两次照搬西方军事制度。<br />
<br />
　　第一次照搬是在甲午战争失败后。清政府决定按照西法编练新军。这是最早中国对西方军事制度的学习，引进了西方军队先进技术和管理经验，对促进中国军队的近代化起到了积极作用。但也不可否认，在这一学习中，中国固有的“兵归将有、兵随将走”领兵传统在效仿西法的条件下不仅没有削弱，反而得到了进一步强化，军权被个人所掌握，形成了以袁世凯为首的北洋军阀集团。这是辛亥革命失败的重要原因，也是辛亥革命后形成军阀割据和混战的局面的症结所在。如何铲除军阀政治，废除军权私有的军事领导制度，成为辛亥革命后中国的主要问题之一。在苦苦的探索中，革命先行者孙中山把目光从西方转向了十月革命后的俄国。他在改组国民党、实行国共合作的同时，参照苏联军制成规组建新的军队，在军队中设立党代表和政治部，建立革命的党军。这是党领导军队这种新型军事领导制度在中国的最初萌芽。<br />
<br />
　　第二次照搬是在蒋介石背叛革命后。蒋介石政变得手后不仅背叛孙中山的联俄联共扶助农工的革命政策，还把刚刚实行的包括党领导军队在内的新制度扼杀在摇篮中。他先是以德国的方式改造部队，其后又照抄美国的军事制度。但这都无法改变其军队内部各自为政、画地为王、互相火并的局面，军队又成为各派军阀争权夺利的工具。<br />
<br />
　　与照搬西方军事制度造成惨败结果形成鲜明对照的是，中国共产党在总结苏联和大革命经验教训的基础上，创立了党对军队绝对领导的根本原则和制度。这是中国军事制度亘古未有的伟大变革，使中国军队面貌发生了根本性变化，对中国近现代史产生了深远影响：军队成为完成党的政治任务的武装集团，真正结束了枪杆子由个人掌握的历史，从根本上消除了军阀政治在军队生长的土壤，使中国摆脱了军阀操纵政党及政治的梦魇；军队由个人或狭隘利益集团的工具转而成为人民和国家的武装，人民军队成为中国有史以来最进步的队伍；军队各级党组织成为坚强的政治核心，保证了部队的高度集中统一，自建军以来没有一支成建制的队伍被敌人拉过去，没有任何个人野心家能够利用军队达成其目的，在重大政治关头，我军总是与党中央在政治上保持高度一致；把对部队的政治掌握和军事要求、党的组织和军队建制有机地统一起来，有效地保证了各项使命任务的完成。所有这些，都是照搬西方军事制度所无法想象和企及的，这充分说明了党对军队绝对领导制度的进步性和科学性。<br />
<br />
　　新中国成立已过一个甲子。纵观中国历史，一个朝代在其开国后的60年里，军队几乎没有不发生问题的。在当今世界，不少发展中国家最头痛问题也是兵权掌控的问题，军事变乱已成为影响其发展进步的重要因素。中国历史上发生的情况，一些发展中国家出现的问题，始终没有在新中国再现。其重要原因，就是邓小平同志深刻指出的：“我们国家所以稳定，军队没有脱离党的领导的轨道，这很重要。”<br />
<br />
　　美国知名政治学家塞缪尔・亨廷顿提出过“制度化水平”的概念，这是一个十分有意义的发展政治学概念。其基本含义是：衡量一种政治制度制度化水平的高低，关键要看这个制度对于社会的适应性，越适应于社会实际、适应于社会发展的要求，它的制度化水平就越高。从这个意义上说，坚持党对军队绝对领导的基本军事制度，有利于运用国家政权的力量确保国家的长治久安，有利于保证军队的最高领导权和指挥权的高度集中统一，有利于永葆人民军队的性质、宗旨和强大战斗力，有利于发挥军队在保卫和建设国家中的职能与作用，是符合中国国情军情、具有较高“制度化水平”的科学有效的制度。<br />
<br />
　　一段时间以来，西方敌对势力大肆鼓噪“军队非党化、非政治化”和“军队国家化”，把西方军事制度这一被中国历史淘汰的旧货重新炒热端出，这既脱离了中国的历史条件，又脱离了中国的现实情况。其险恶用心就是企图用西方的军事制度和治军模式取代党对军队绝对领导，改变我军性质，进而为改变我国社会政治制度打开通道。如果真的按其主张去做，在中国的条件下，我们可能又要回到过去那种个人操纵军队甚至军阀政治、军阀混战的老路上去，这不仅是中国军事制度的倒退，也必然是中国历史的倒退。<br />
<br />
　　我们在军事制度问题上同国内外敌对势力的斗争，从根本上说，是维护国家民族权益的问题，是巩固我国政治安全的问题，也是维护我们正确发展道路的问题。在这个事关国家民族命运的重大问题上，一定要始终保持高度的警惕和清醒的认识。<br />
<br />
　　<strong>始终不渝地坚持和与时俱进地发展的统一――在实践中坚持和完善党对军队绝对领导的一系列制度</strong><br />
<br />
　　历经80多年风雨沧桑，我们党形成和确立了一整套稳固、成熟的党对军队绝对领导的根本制度。其主要内容包括：军队的最高领导权和指挥权集中于党中央、中央军委；部队各级党委坚持贯彻民主集中制的组织原则；实行党委统一的集体领导下的首长分工负责制；团以上单位设立政治委员和政治机关制度；支部建在连上等。这些制度构成了一个严密、科学、完整的体系，实现了党的组织与军队建制的紧密结合，实现了党的领导与军事行政领导的内在统一，是坚持和贯彻党对军队绝对领导的根本保证。无论战争形态怎么发展，体制编制怎么调整，环境任务怎么变化，我们都必须坚定不移地、一以贯之地坚持下去。<br />
<br />
　　坚持党对军队绝对领导的根本制度，必须进一步强化“军魂”意识。“魂者，器物之统摄也”。“魂”乃我军的生命所系，无“魂”则无“命”；“魂”乃我军的立军之本，无“魂”则无“本”；“魂”乃军队建设发展的方向和动力源泉，无“魂”则迷“向”。我们必须自觉坚持用党的创新理论武装官兵头脑，充分认识党对军队绝对领导这一基本军事制度的历史必然性和极端重要性，始终保持和发扬听党指挥、服务人民、英勇善战的优良传统。<br />
<br />
　　党对军队绝对领导是根本原则和制度的绝对性与具体制度和实现形式相对性的辩证统一。党对军队绝对领导的根本制度，既要始终不渝地坚持，又要与时俱进地发展，努力使党对军队绝对领导的根本制度始终保持生机活力。要正确处理继承与发展、借鉴外军经验与保持我军特色、“毫不动摇”与“与时俱进”的关系，积极探索完善党对军队绝对领导的具体实现形式和途径，在努力学习、掌握和运用一切科学的新思想、新知识、新经验的创造性实践活动中，不断赋予这一制度以新的时代内涵。<br />
<br />
　　划清中国特色基本军事制度与西方军事制度的界限，并不意味着我们排斥学习借鉴国外一切反映时代要求的科学有益的东西。外军有益的管理经验符合世界军事发展的共同规律，不属于基本制度范畴。要积极适应世界军事发展的新趋势和我军发展的新要求，大胆吸收借鉴外军的有益管理经验，既坚决抵制“建设信息化军队离不开外军模式”的错误思潮，又善于把外军有益的经验从其基本军事制度范畴中剥离出来，逐步形成一整套既有中国特色又符合军队建设规律的科学的组织模式、制度安排和运作方式，跟上并引领时代发展的潮流。</div>

							
							
							
							
