看吧，“衣冠不整”的人，还在天天的抓，抓去打内战；老百姓开会，特务就去捣乱，动不动就要拆草棚子。这类事情数不清。人民“安居”了吗?大学教授逢场就去摆地摊、卖东西，工人时时会失业，失业后又不准要求救济，还要抓代表；官办工厂也拖欠工资，工人不能去要，倒反要被抓去坐牢，人民“乐业”了吗?这是因为军队没有国家化呢，还是因为人民的自由权利被国民党一党政府剥夺得干干净净呢?只要顾事实的人，都将异口同声说一句，不能安居乐业，是因为没有民主自由啊!<br />
<br />
他们又说了，英美苏都没有实现民主理想，中国来实现，就“不切实际”了，更何况中国是在训政和宪政之间呢?我们再来看看事实吧。英美苏等民主国家，都已做到人民有言论出版集会结社和选举等自由权利，中国不必“开风气之先”。可是，曾否追随“世界上任何先进国家”之后了呢?他们又提到罢工、游行、示威的自由，世界上任何先进国家所未曾做到；教授治校是理想；就是罗斯福总统说的四大自由的一半，“不虞匮乏”、“不受恐怖”两大自由，都“不能便没有疑问”。反对自由者的话，是够露骨的了。<br />
<br />
在民主国家，罢工游行示威，几时未曾做到?举美国为例，罢工不仅在发展，政府还根据康纳利法案，承认罢工是合法的。游行示威不但不被禁止，还可以请政府派警察保护。至于教授治校，不受党化，过去北京大学清华大学与现在的西南联大，就是经过教授会议来处理校务，这与其说是理想倒不如说是教育上优良制度的确立。<br />
<br />
说到四大自由，在今天以前，除了法西斯独裁者外，倒还没有听见有人反对过。现在重庆还是第一次看见公然对“不虞匮乏”和“不受恐怖”的自由提出疑问。如此说来，人民只应有匮乏的自由了，穷人王正兴必须卖儿子，陈望道教授必须摆地摊，倒成了自由的享受；而政府逼人民匮乏，倒成了政府的“合法”自由；至于受恐怖也成人民的自由，所以，“衣冠不整”的老百姓，只有受恐怖被拘捕，失业工人只有坐牢的恐怖，吴金山也只有被押起来了。特务横行，便成了政府执行恐怖自由了。<br />
<br />
正告发表这些违背人民利益、维护独裁专政的谬论的人们，也正告反人民反民主的当局，匮乏和恐怖的自由，是人民所坚决反对的，这条路是死路，不好再往前走了。人民要求和争取四大自由，是没有力量压得下挡得住的。这些一切，只有证明全国人民及各民主党派对实施纲领的意见，首先是对人民自由的主张，是切实的，迫切需要实现的，万万“撤销”不得的。&nbsp;&nbsp;<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
――《新华日报》1946年1月18日</div>

							
							
							
							
