													<h2>工信部将推境外域名“白名单”制度</h2>
						
						
																											<div id="postmessage_385301" class="t_msgfont">from 法国国际广播电台<br />
作者 上海特约记者 沈愚&nbsp;&nbsp;发表日期 20/12/2009<br />
<br />
<br />
中国政府近日启动了全国性的域名注册服务管理专项规范整治行动。<br />
<br />
12月16日，中国工业与信息化部组织全国域名注册服务及管理机构，在北京召开专项行动会议。会上传出消息，中国除了不解析未备案域名外，境外注册的国际域名将不得用于中国业务。<br />
<br />
业界人士预计，这意味着中国互联网将实行前所未有的“白名单”制度。一旦该措施大规模推广，除了少数国际性的大网站，没有主动“备案”，或者无法通过中国政府“备案”的境外网站，将从技术上无法解析域名，在中国无法访问。<br />
<br />
会议透露，官方将通过一系列的技术手段，对部份已建站的在境外注册的域名做监管，并对国外的注册商实施相应的管制措施，杜绝中国网站利用境外注册域名在境外建站来逃避打击。<br />
<br />
同时会议还讨论了有关境外企业开展中国业务时所使用的网站的相关管理草案，今后将对一些在中国开展业务的外国企业的域名和网站实施监管，这些企业在中国开展业务所用的域名必须通过国内的注册商注册，在境外注册的域名将不得用于开展中国业务。<br />
<br />
据中国官方新华社报道，工信部要求域名注册管理机构、注册服务机构采取措施规范域名注册。<br />
<br />
工信部电信管理局副局长刘杰透露，五项措施包括：建立域名持有者黑名单机制，将被关闭网站域名持有者纳入黑名单，防止违规网站重新申请域名；域名申请者应提交真实、准确、完整域名注册信息，域名转让必须重新注册，违反者将注销域名；不予解析未备案的域名；重点清理域名注册管理机构、域名注册服务机构在业务推广渠道中业务合作伙伴、合作方式、业务推广模式和网络连接方式存在的问题。<br />
<br />
官方还要求，如果网站被认为涉黄和违规，域名服务商必须配合停止域名解析，将域名持有者的全部其他域名暂停解析，并将其纳入黑名单。</div>

							
							
							
							
