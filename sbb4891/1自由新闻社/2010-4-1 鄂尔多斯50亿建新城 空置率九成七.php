																											<div id="postmessage_550870" class="t_msgfont">鄂尔多斯市（蒙古语：&#6179;&#6199;&#6195;&#6179;&#6192; &#6188;&#6179;&#6194;&#6176; Ордос хот），即前伊克昭盟（蒙古语：&#6198;&#6177;&#6202;&#6177; &#6197;&#6179;&#6180; Их Зуу yeke jou），是内蒙古自治区下辖的一个地级市。其名称来自所处的鄂尔多斯高原，政府所在地东胜区。<br />
