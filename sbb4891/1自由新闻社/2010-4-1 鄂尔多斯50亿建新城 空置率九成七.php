　　耗资50多亿打造、面积达32平方公里的内蒙古康巴什是一座豪华新城,但同时也成了一座无人居住的'鬼城’。花5年时间建设的康巴什,原意要成为鄂尔多斯对外炫耀的市中心,但如今却是中国房地产泡沫的最佳展示品。<br />
　　偶尔出现的行人,看起来就像幻觉,拖着沉重的脚步沿着人行道走着,仿佛恐怖电影中大灾难过后一名孤独的幸存者。<br />
　　在中国的版图上,有个地方三面被黄河环绕。十三世纪时,一代天骄成吉思汗途经此处,将这里定为他的长眠之地。这里的蒙语地名是“鄂尔多斯”,汉语意为“众多的宫殿”。<br />
　　GDP连年攀升、增长竞争力位列全国第一的鄂尔多斯,凭借丰厚的经济实力,在一片荒漠中建起一座新城――名叫康巴什。<br />
　　只是,地图上的这个新地名,不仅是座新城,也是个“空城”――一期工程方圆32平方公里的城市,街道上鲜见行人,没有人气。<br />
<br />
　　街上的清洁工比行人多<br />
<br />
　　“康巴什是个空城。晚上没人,白天人也很少。”<br />
　　3月14日晚7时许,内蒙古自治区鄂尔多斯市东胜区郊外55公里处的鄂尔多斯机场。刚下飞机,记者向一名工作人员打听“康巴什在哪里”,对方这样回答。<br />
　　康巴什距离东胜区22公里。荒漠中,一条一级路面的大道连接两地。<br />
　　15日上午,路上的车辆并不多。刚出市区,还能看到路旁有不少在建的楼盘,越往南越发荒凉起来。<br />
　　路的尽头陡然出现了大片林地,还有一栋栋楼房、一排排别墅,很是气派。上午10点,进入康巴什新区,第一个十字路口的红灯亮起时没有别的车停下来,横向的绿灯下也没有车通过,也看不到行人。<br />
　　康巴什的中心位置是鄂尔多斯市党政大楼,3栋外形一致的12层楼房。朝南正对着的是这个新区的南北中轴线――宽200米、长2400米的成吉思汗广场。<br />
　　广场西北角、鄂尔多斯大街与文化西路路口,清洁工老李正在清扫路面。这是记者在康巴什遇到的第一个人。<br />
　　她负责的路段是从这里朝西数10根电线杆的距离。500多米的路段,老李扫了几年。头两年,“更好打扫,啥也没有,除了沙土,还是沙土。”她随手拎着个不大的塑料袋,里面装着多半袋杂物,“两天时间就只扫了这么点垃圾,另外,还捡到1个瓶子。”“为啥没垃圾?因为没人呗。”她一边划拉扫帚一边问记者“几时来的”,并建议说:“你晚上再来看看,吓人呢――都是黑洞洞的”。<br />
<br />
　　50多亿打造的荒漠新城<br />
<br />
　　正值春季,不时刮着四级风。<br />
　　16日下午5时许,成吉思汗广场最南端的景观湖,看风景的只有记者一人。<br />
　　湖边一角的公共卫生间小休息室里,挤着6名清洁工,山东夫妇、湖北夫妇,另外两个老头是内蒙古当地的。去年才来康巴什打工的山东女人说:“这里除了车,就是楼,可就是没人。”当地一个老头接过话:“康巴什车头比人头多”,大家都笑起来。<br />
<br />
　　2004年以前,康巴什还是片荒漠,只有两个小村庄,不到1400人。<br />
　　那时,整个鄂尔多斯一度是内蒙古最为贫困的地区。直到有一天,这里发现了四大“宝贝”:特有的阿尔巴<br />
　　斯白山羊绒被誉为软黄金;煤炭探明储量1676亿吨,占全国1/6,鄂尔多斯如今是中国产煤第一大市;稀土储量65亿吨;天然气探明储量 8000多亿立方米,占全国1/3。<br />
　　因为“羊、煤、土、气”,于是扬眉吐气。<br />
