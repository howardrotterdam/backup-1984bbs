[编辑] 地理<br />
面积87890平方公里。鄂尔多斯高原是地形主体。气候干燥，属沙漠型气候，境内主要是毛乌素沙漠。<br />
<br />
黄河流经本市北边，沿岸是本市的精华区。<br />
