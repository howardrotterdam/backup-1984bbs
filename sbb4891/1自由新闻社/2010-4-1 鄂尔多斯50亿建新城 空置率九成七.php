<strong>　　东北新闻网4月1日报道</strong> 耗资50多亿打造、面积达32平方公里的内蒙古康巴什是一座豪华新城，但同时也成了一座无人居住的‘鬼城’。花5年时间建设的康巴什，原意要成为鄂尔多斯对外炫耀的市中心，但如今却是中国房地产泡沫的最佳展示品。<br />
<br />
　　偶尔出现的行人，看起来就像幻觉，拖着沉重的脚步沿着人行道走着，仿佛恐怖电影中大灾难过后一名孤独的幸存者。<br />
<br />
　　在中国的版图上，有个地方三面被黄河环绕。十三世纪时，一代天骄成吉思汗途经此处，将这里定为他的长眠之地。这里的蒙语地名是“鄂尔多斯”，汉语意为“众多的宫殿”。<br />
<br />
　　GDP连年攀升、增长竞争力位列全国第一的鄂尔多斯，凭借丰厚的经济实力，在一片荒漠中建起一座新城――名叫康巴什。<br />
<br />
　　只是，地图上的这个新地名，不仅是座新城，也是个“空城”――一期工程方圆32平方公里的城市，街道上鲜见行人，没有人气。<br />
<br />
<strong>街上的清洁工比行人多</strong><br />
<br />
　　“康巴什是个空城。晚上没人，白天人也很少。”<br />
<br />
　　3月14日晚7时许，内蒙古自治区鄂尔多斯市东胜区郊外55公里处的鄂尔多斯机场。刚下飞机，记者向一名工作人员打听“康巴什在哪里”，对方这样回答。<br />
<br />
　　康巴什距离东胜区22公里。荒漠中，一条一级路面的大道连接两地。<br />
<br />
　　15日上午，路上的车辆并不多。刚出市区，还能看到路旁有不少在建的楼盘，越往南越发荒凉起来。<br />
<br />
　　路的尽头陡然出现了大片林地，还有一栋栋楼房、一排排别墅，很是气派。上午10点，进入康巴什新区，第一个十字路口的红灯亮起时没有别的车停下来，横向的绿灯下也没有车通过，也看不到行人。<br />
<br />
　　康巴什的中心位置是鄂尔多斯市党政大楼，3栋外形一致的12层楼房。朝南正对着的是这个新区的南北中轴线――宽200米、长2400米的成吉思汗广场。<br />
<br />
　　广场西北角、鄂尔多斯大街与文化西路路口，清洁工老李正在清扫路面。这是记者在康巴什遇到的第一个人。<br />
<br />
　　她负责的路段是从这里朝西数10根电线杆的距离。500多米的路段，老李扫了几年。头两年，“更好打扫，啥也没有，除了沙土，还是沙土。”她随手拎着个不大的塑料袋，里面装着多半袋杂物，“两天时间就只扫了这么点垃圾，另外，还捡到1个瓶子。”“为啥没垃圾？因为没人呗。”她一边划拉扫帚一边问记者“几时来的”，并建议说：“你晚上再来看看，吓人呢――都是黑洞洞的”。<br />
<br />
<strong>50多亿打造的荒漠新城</strong><br />
<br />
　　正值春季，不时刮着四级风。<br />
<br />
　　16日下午5时许，成吉思汗广场最南端的景观湖，看风景的只有记者一人。<br />
<br />
　　湖边一角的公共卫生间小休息室里，挤着6名清洁工，山东夫妇、湖北夫妇，另外两个老头是内蒙古当地的。去年才来康巴什打工的山东女人说：“这里除了车，就是楼，可就是没人。”当地一个老头接过话：“康巴什车头比人头多”，大家都笑起来。<br />
<br />
　　2004年以前，康巴什还是片荒漠，只有两个小村庄，不到1400人。<br />
<br />
　　那时，整个鄂尔多斯一度是内蒙古最为贫困的地区。直到有一天，这里发现了四大“宝贝”：特有的阿尔巴斯白山羊绒被誉为软黄金；煤炭探明储量1676亿吨，占全国1/6，鄂尔多斯如今是中国产煤第一大市；稀土储量65亿吨；天然气探明储量8000多亿立方米，占全国1/3。<br />
<br />
　　因为“羊、煤、土、气”，于是扬眉吐气。<br />
<br />
　　2007年年底，中国城市竞争发展力排名：鄂尔多斯增长竞争力全国第一；人均GDP1.0451万美元，超过北京、上海。<br />
<br />
　　鄂尔多斯老城区东胜23平方公里上拥挤着30万人。手中有了钱，政府便开始拓建新的城市发展空间。2004年，政府开始在荒漠中开始兴建新区，建设总投资达50多亿元。<br />
<br />
　　站在党政大楼12层楼顶往南看，在这张巨大的荒漠“白纸”上，矗立着康巴什的六大地标性建筑：剧院、文化艺术中心、会展中心、新闻中心、博物馆、图书馆。<br />
<br />
　　只是，这里还缺少超市、医院、学校和人。<br />
<br />
　　尽管各种设施还在完善中，但鄂尔多斯人对这座新城依然充满自豪，把康巴什誉为“千年荒漠、创业热土”。新区党工委季刊《康巴什》2009年第1期、署名蔺怀恩的一篇文章写道：“眼前这条舒畅伸展的大道，便是人们惊呼的‘上班大道’了……因为市政府机关刚搬迁不久，商业的经营尚属空白，几乎是机关人员的来回跑道。现在是早上8点多钟，赶班点的汽车们轰轰烈烈……我乘坐的这辆沃尔沃轿车身前身后是一片车的涌流。”“奔驰、保时捷、宾利、法拉利们跑得气咻咻，一个个志在超越；后边的宝马、福特野马、兰博基尼、凯迪拉克们你追我赶，使着绝不甘于居后的情绪；就是那享有豪族尊位的劳斯莱斯、阿斯顿马丁也是那种飞翔的姿影，正在望风绝尘、不可一世地冲来……”<br />
<br />
<strong>令人费解的荒漠“白纸”</strong><br />
<br />
　　公务员是这里人口构成的最主要部分。鄂尔多斯市委宣传部外宣科科长陈曦告诉记者，2006年7月31日，党政机关从东胜老城区正式迁到康巴什新区，但很多公务员家还在东胜，“于是两头跑。”<br />
<br />
　　再就是到政府办事的人。16日，在康巴什新区的政务服务大厅里，记者终于见到几天来康巴什人口密度最稠密的一刻：300多平方米的大厅里，30多名工作人员、办事的50多人。<br />
<br />
　　15日，中午时分，图书馆大厅。大厅一侧是新华书店，4个正在看书的人，其中两人是工作人员。而大厅里的保安比读者多，足足有5人。<br />
<br />
　　康巴什新区到底有多少人？2009年8月出版的《康巴什》季刊透露：新区人口情况2008年为28000人，最新的人口统计数据为2.86万人。<br />
<br />
　　两三万人散落在一个32平方公里的钢筋水泥丛林里，不难想象是个什么场景。<br />
<br />
<strong>“真空地带”的未来</strong><br />
<br />
　　这样一个全新的城市，似乎也蕴含无数商机。记者从康巴什新区管委会了解到，至今，康巴什累计注册企业近350家，注册资金近百亿元。经济迅速增长，造就了这里的高房价。4星级恒信大酒店，豪华套间每晚1188元，单人间398元。<br />
<br />
　　商铺的租金更是居高不下。康城一期3号楼一家粮油蔬菜店，90多平方米，年租金10万元，水电费另计。<br />
<br />
　　老张是去年8月来康巴什开店的，当时听说很多煤老板在这里购房，“人气一定很旺的”，谁知大半年了，当地住户没见着几个。老张只好把生意瞄准各家工地，给农民工批发粮油蔬菜。<br />
<br />
　　撑不住的就准备“撤”了。B10区3号楼一层的一家饭馆大门紧锁，贴着“转让”两字。大多数商铺似乎都在“做长线”，等待旺盛人气的出现。<br />
<br />
　　“没有人气，新区就是一座‘空城’，处于‘真空’地带。”康巴什新区管委会主任蔺伟在今年管委会第一次会议上对此也并不讳言。尽管现在没有多少人，但康巴什的未来仍“不可限量”，新区二期建设已经开始。<br />
<br />
　　而新区管委会最新布置的宣传牌显示，将来康巴什面积将达352平方公里――这将是目前规模的10多倍。<br />
<br />
<strong>外媒关注 </strong><br />
<br />
<strong>中国地产泡沫催生“鬼城”？</strong><br />
<br />
　　美国《时代》周刊4月5日（提前出版）刊发文章《鬼城》称，没有比这个问题更让众多经济学家、投资者和银行家们夜不能寐的了：中国房地产市场是泡沫吗？<br />
<br />
　　多年来，中国各地上马大量房地产项目，吸引了私人及公司的购买者。随着房价持续上涨，更多投资者变成投机者，他们买入崭新房屋只有一个目的，等高价时抛出。自美国房地产业崩盘变成全球经济衰退催化剂以来，很多人士担心这一幕在中国重演将是灾难性的。事实上，供给过剩的证据到处都是。在北京，一幢幢商业楼盘空置着。但如果深入中国内地，会有更加怪异的情景让人无法乐观：为数百万居民建造的城市耸立着，却成为了一座“鬼城”。<br />
<br />
　　或许最荒凉的鬼城是内蒙古的康巴什。最初为100万人居住、生活和娱乐而设计的这个地方却几乎没有人居住。只有几辆汽车驶过多车道公路，白天有些政府办公室开门办公。偶尔出现的行人，看起来就像幻觉，拖着沉重的脚步沿着人行道走着，仿佛恐怖电影中大灾难过后一名孤独的幸存者。<br />
<br />
　　只用了5年时间就建成的康巴什，设计初衷是要成为鄂尔多斯对外炫耀的市中心，但如今却是中国房地产泡沫的最佳展示品。<br />
