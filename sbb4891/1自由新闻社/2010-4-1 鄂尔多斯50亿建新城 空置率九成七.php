[编辑] 历史<br />
“鄂尔多斯”为蒙古语，汉语意为“众多的宫殿”，是成吉思汗守陵部落名称。战国时代时为赵国领土，后属秦国。汉初为匈奴与汉征战的前线。汉武帝时在此设立朔方郡。汉宣帝时呼韩邪单于来朝，成为南匈奴的居住地，后渐胡汉杂居，于西晋时爆发五胡乱华。五胡十六国时曾为前秦、后秦领土。北朝时属北魏、西魏、北周。隋唐时皆为领土，唐代曾置朔方节度使，名将郭子仪曾任职此职。安史之乱时，唐肃宗曾逃至此地。宋代为西夏国领土，元代时归陕西行中书省，明代时为长城塞外。清时属漠南蒙古的一部分，后置归绥道，属山西省，清朝顺治六年（公元1649年），鄂尔多斯各旗在达拉特旗规模宏伟的王爱召会盟，形成了“伊克昭盟”（汉意为大庙），其行政建制一直延续到本世纪初。民国后设绥远特别区，后改为绥远省，设伊克昭盟。中共建制后，划入内蒙古自治区至今。2001年经国务院批准撤消伊克昭盟，成立鄂尔多斯市。<br />
