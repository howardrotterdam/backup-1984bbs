<br />
空置率逾 97%<br />
<br />
<img src="http://pic1984.12hp.de//up/1270108665.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
图书馆每日几乎不见读者光顾。<br />
<br />
国内生产总值（ GDP）连年攀升、增长竞争力位列全国第一的内蒙古中部城市鄂尔多斯，凭借丰厚的经济实力，在一片荒漠中建起一座新城镇。但这座一期工程方圆 32平方公里的新城镇，如今更像「空城」──街道上鲜见行人，没有人气。<br />
<br />
康巴什昔日曾是一片荒漠，只有两个小村庄，不到 1,400人居住。内蒙古自治区政府于是在 2004年开始建设康巴什，目标是大造可容纳 100万人的新城镇，并将它定位为鄂尔多斯的政治文化中心。因此住宅、商业楼宇满布市内，同时博物馆、电影院等设施随处可见。<br />
<br />
但整个城镇建设至今，仅 2.8万多人入住，繁华，宽阔、笔直的马路上间中仅零星汽车驶过，住宅区内空无一人，别墅区空空荡荡……，巨大的成吉思汗广场只有清道夫扫沙尘。但令人感到讽刺的是，整个城镇最热闹的竟然是建筑地盘。虽然，整个新城镇的空置率逾 97%，但地产商仍未停止发展，兴建更多住宅。<br />
<br />
鄂尔多斯因丰富煤矿资源，近几年「一夜暴富」，从全内蒙最穷地区变为号称人均 GDP已经超越香港的城市。当地政府因此头脑发热，建设康巴什，拟将它打造成为鄂尔多斯的新城镇，成为向世人炫耀的产品。当地靠煤致富的富豪们和外资，在看好康巴什的发展前景下，近年大肆扫货，囤积当地的大批楼盘，但它最终成为楼市泡沫最佳示范品。<br />
