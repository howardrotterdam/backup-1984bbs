																											<div id="postmessage_543161" class="t_msgfont">Apple Daily 苹果日报网上版 －－2010-04-01<br />
内蒙 50亿图建繁华小区<br />
富豪囤积楼盘　新城镇变鬼城<br />
<br />
<img src="http://imgur.com/5qpAX.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
整个城镇渺无人烟，偶而有人路过的场面亦不多见。<br />
<br />
内地楼价持续攀升，引发大批蜗居族的同时，内蒙古一个可容纳 100万人居住的新城镇，因富豪囤积大批楼盘而遭闲置。巨大的城镇俨如死城。美国《时代》（ Time）周刊指，内蒙古鄂尔多斯市花逾五年时间、耗资逾 50亿元（人民币．下同）建设的新城镇康巴什，原意要成为该市对外炫耀的发展项目，但如今却是房地产泡沫的最佳展示品。<br />
<br />
「康巴什是个空城。晚上没人，白天人也很少。」有刚抵达鄂尔多斯市的记者一下飞机，打探康巴什的位置时，当地居民竟如此坦率地对外来人说。<br />
<br />
<img src="http://pic1984.12hp.de//up/1270108629.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
