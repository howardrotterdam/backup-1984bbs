													<h2>鄂尔多斯&quot;鬼城&quot;房价每平6千 居住者多为公务员</h2>
						
						
																											<div id="postmessage_561737" class="t_msgfont">鄂尔多斯&quot;鬼城&quot;房价每平6千 居住者多为公务员<br />
2010-04-13 10:43:13　来源: 东北网(哈尔滨)　<br />
核心提示：日前，媒体报道了鄂尔多斯“鬼城”康巴什，这座鬼城耗资五十亿打造，建好后却成了一座空城。据悉，当地住户多为公务员，房价达到了每平方米6000元。市场人士称，当地炒房团身影活跃，楼市存在泡沫，但总的来看，至少三五年内，当地房价还会持续涨。<br />
耗资50多亿打造、面积达32平方公里的内蒙古康巴什是一座豪华新城，但同时也成了一座无人居住的‘鬼城’。鄂尔多斯是内蒙古一个相对比较富裕的煤矿业中心，常住人口150万。康巴什的基础设施一应俱全，有办公大厦、行政中心、政府建筑、博物馆、电影院和运动场，中产阶级式的复式公寓和别墅将成片的小区塞得满满当当，但现在的问题是，原本是用来安置100万人口的康巴什却很少有人入住。<br />
<br />
 <br />
<br />
一位行人走在几乎闲置的商业区，因为人少，几乎没有人愿意在这块新区做生意。<br />
<br />
东北网4月13日报道 鄂尔多斯陷入了“话语”的沙尘暴。<br />
<br />
外媒一篇报道质疑，鄂尔多斯是一座现代化的空城，作为100万人新家的康巴什新区鲜有人居住。<br />
<br />
记者在康巴什新区看到，15分钟过去，不见一个行人，驶过的汽车不到10辆。阳光在辽远天空下显得有些停滞了。<br />
<br />
3栋12层的党政大楼正对着宽200米、长2400米的成吉思汗广场。成吉思汗和骏马的巨大雕塑，使整个广场尽显气势。广场南端矗立着新区六大地标性建筑，参照蒙古包形式设计的大剧院、书籍样式的图书馆和以岩石为原型的博物馆等。一期建设32平方公里的康巴什新区，目前只有人口2.8万。<br />
<br />
“把康巴什称作‘鬼城’，是不负责任的说法。”鄂尔多斯市康巴什新区管委会副主任杨满喜略带愤懑地对本报记者表示。这也是鄂尔多斯官方首次回应外界“鬼城”的说法。<br />
<br />
康巴什因何在沙漠中崛起？有当地人士告诉记者，东胜老城区缺水是城市搬迁的原因。而无法忽略的是，这座城市因煤而富，其人均GDP直逼香港，雄厚的财政实力才是沙漠新城崛起的背后推手。<br />
<br />
正是这样一座“空城”，其新开楼盘的房价已达6000元每平米。与沿海一线城市一样，这里也充斥着炒房团的身影。但杨满喜4月7日对本报说，康巴什新区目前入住率低，不能说明房地产市场就存在泡沫。<br />
<br />
鄂尔多斯，因资源而勃兴，随之刺激楼市的繁荣。这座人均GDP居于国内前列的城市需要思考的是，城市运营是否能够建立在资源与楼市泡沫的沙滩上。<br />
<br />
“空城”中的高房价 <br />
<br />
“‘鬼城’是什么意思？就是不是人住的地方。那么你来看这里是这样吗？”杨满喜问到。4月7日的鄂尔多斯阳光明媚，风力3-4级。在康巴什新区，西北风中夹杂着些许沙尘，在路上行走不一会，嘴里便能感觉到。<br />
<br />
鄂尔多斯人以能在沙化地上如此迅速的建起这座新城感到自豪。2004年前，康巴什还只是片荒漠，两个小村庄不到1400人。2004年新区基础设施建设开始，随后房地产开发陆续展开，鄂尔多斯市党政机关也在2006年7月搬迁至此。<br />
<br />
一面临山，三面环河，新城区风水极佳。由快速通道进入康巴什后的景观大道将新区一分为二。<br />
<br />
党政机关、公共设施和居住区多位于西侧。党政大楼的背后是成片的西式别墅向远处延伸，看不到有人居住的迹象。除去办公区域，造型现代的公寓、连片的住宅更是随处可见。<br />
<br />
宽阔的马路上车多人少，依稀环卫、建筑工人各自劳作。因为是工作日，各党政机关门外室内倒是往来频繁。<br />
<br />
景观大道以东则多是产业园区，至今累计注册企业350家。新区东北部还规划有文化创意产业园。这项名为“鄂尔多斯100”的项目2008年启动，将由世界100名设计师设计100栋别墅作为住宅，并建设酒店、工作室等。但是在这超过2平方公里的区域内，虽然设计模型已经有了，但在建的屈指可数。<br />
<br />
杨满喜也承认说，现在居住在新区的还多是公务员。<br />
<br />
杨满喜不喜欢外界“空城”的说法。他说，老城东胜区70万人从无房到有房并不容易。原先住的公房，或者是没房，后来鄂尔多斯人开始富裕起来，市民才从有一套房到两套以上的房子。<br />
<br />
“现在也还没有满足需求。”杨满喜说，新区的住宅已经都售出，也多为鄂尔多斯人购买。虽然现在入住率低，但这是“阶段性的正常现象”，“一年后、两年后你再来看，肯定不是现在这样”。<br />
<br />
康巴什新区目前新房均价在6000元左右，已经超过内蒙古省会城市呼和浩特。在呼市的内蒙古大伟海润房地产公司销售总监魏东对本报说，康巴什的商品房的确都卖了，但现在没人住。“市政府从东胜搬迁至新区，也是希望能形成新的居住区，带动新城发展。”<br />
<br />
位于鄂尔多斯市一中对面的康和盛世小区地理位置优越，下月才开始预售，上门咨询的人就络绎不绝。小区属鄂尔多斯本地开发商东胜蒙欣房地产公司，共600套售6000-7000元每平米。<br />
<br />
置业顾问杨阳4月8日对本报说，前来咨询的人中本地的居多，自住和投资需求大约各占一半。即便对于工薪阶层的购房者来说，价格偏高，但她看好小区的销售前景。“鄂尔多斯有购买力的人太多了。”而附近领一小区翰林苑在2008年开盘不久就销售一空，“新区造好的房子基本都买完了”。<br />
<br />
鄂尔多斯另一家经营东胜区旧城改造项目的开发商的销售总监也对本报说，鄂尔多斯人有置业的传统，而在老城区的改造中，也催生出每年3至4万人的购房需求；除了回迁，就是另买新房。“所以不能说康巴什新区就没有刚性需求。”<br />
<br />
而在鄂尔多斯市的规划中，32平方公里的新区一期建设还不能满足需求，二期建设也已开始。<br />
<br />
但杨满喜纠正道，二期建设并不是此前媒体报道的352平方公里，而是70平方公里；其中也只有10%是建设用地，其余90%是绿地和公园用地。“352平方公里是包括县(乡)在内的康巴什总体面积。”<br />
<br />
新城崛起背后推手 <br />
<br />
康巴什新区开发的动因也并非单纯出于带动经济发展的考虑，原东胜老城区缺水也是重要原因。鄂尔多斯的市区构成包括三部分，东胜区、康巴什新区和伊金霍洛旗。<br />
<br />
由于库布其和毛乌素两大沙区占鄂尔多斯市国土面积的40%，原市政府所在地东胜区年降水量不足400毫米。东胜成为我国北方缺水严重的城市之一。在目前东胜区人口接近80万的背景下，用水紧张也成为城市搬迁新建的原因。<br />
<br />
“鄂尔多斯是漏斗型城市之一。当初开发新城，可能也有东胜缺水的因素。”内蒙古社会科学院经济学研究室研究员孙学力对本报说。<br />
<br />
由于地下水超采，城市地下形成漏斗型，造成地下水位逐年降低。而康巴什新区三面环水的地理位置，为新城提供了充足的水源，这也成为新城吸引鄂尔多斯人置业的推动力。<br />
<br />
但是仅有水源显然不够。新城区大规模的基础设施建设需要的巨量资金从何而来？<br />
<br />
首先是煤炭收入。2008年，鄂尔多斯市财政收入265亿元人民币，原煤销售对财政的贡献率是53%。而经济开发区最初的开发方式是企业与政府合作成立投资公司，政府占30%股份，但不注入资金，也不参与分红。企业解决全部资金问题，政府帮助其贷款。而企业则靠经营土地获得了巨大收益。<br />
<br />
杨满喜对本报解释说，新城开发土地的提供通过征地获得。原先康巴什新区所在位置是集体土地。机关办公楼和公共设施通过政府融资平台(城投公司)和银行贷款获得建设资金。其他土地交给开发商建设楼盘，以及作为工业用地出让给企业。<br />
<br />
目前新区存在的问题是，人气不足，配套设施缺乏。新区目前与东胜之间只有一条公交线路，新区内出租车亦极其稀少。簇新的鄂尔多斯一中教学楼陈列在新区南端，却因为配套措施等没有跟上而未启用。而某小区底商也已贴上“转租”的标签。<br />
<br />
针对目前新区人气不足的事实，杨满喜说，管委会正在实施多项措施聚人气：对小区的商业配套给与免税或补贴；尽快建立新区出租车公司，并提供免费的公交系统。“新区配套系统将在2011年全部就绪。”<br />
<br />
孙学力2009年考察过康巴什新区。她说，现在去康巴什的还多是上班族，居住的人还少。虽然新城住宅居住率低，但她认为新城开发是必须的。<br />
<br />
孙学力说，鄂尔多斯是资源(煤炭等)型城市发展的典型，造成新区住宅可能的“供过于求”的问题，并不是产业结构调整所致。问题可能出在新城发展和住宅建设的不配套上，买的人多，自住的人少。<br />
<br />
“在鄂尔多斯，中低收入的人还是占多数。这就是保障性住房建设是否落实到位的问题。”孙学力说。与富有的鄂尔多斯人动辄拥有三四套房相比，新区6000元的均价对于每月三四千元的中低收入人群来说存在一定困难。<br />
<br />
杨满喜补充说，新区已经建有约占总量20%的限价房、经济适用房和廉租房，来解决中低收入群体的住房问题。限价房在3000元左右，经适房在3000元以下，“鄂尔多斯的工薪阶层也有相当的购买力”。而鄂尔多斯还没有符合廉租房条件的人，“但是我们也要准备一些”。<br />
<br />
沙漠炒房团 <br />
<br />
康巴什唱的“空城计”并不妨碍炒房团活跃的身影。<br />
<br />
康巴什新区有没有炒房团？杨满喜的答案是，“没有”。但在市场人士看来，情况可能并非如此，而这也是判断泡沫是否存在的因素之一。<br />
<br />
“新区房子卖得快，是因为鄂尔多斯人大多以投资心理来买房，而非自住。一个人有三四套房很正常。”大伟海润公司销售总监魏东说，“鄂尔多斯楼市的确存在泡沫。但从资金能力、投资取向来看，至少三五年内，当地房价还会持续涨。”<br />
<br />
魏东说，鄂尔多斯的房地产市场是个怪现象，“它不是靠人多(刚性需求)来支撑，而是靠资金来支撑”。他解释说，鄂尔多斯房地产市场是有大把的资金支撑，一是煤炭能源企业资金充足，二是社会闲散资金很多。“有闲钱就扔在这上面。投资心理比较膨胀。”<br />
<br />
2003年之后，伴随着鄂尔多斯煤炭开采高潮的到来，资本迅速向房地产涌入，更是与民间高利贷相互倚重，成为彼此的助推器。鄂尔多斯的房地产市场呈现出火爆的局面。彼时，房地产行业是民间金融的最大目标。<br />
<br />
呼和浩特市厂文房地产经纪公司孙屹曾对本报表示，高峰时期鄂尔多斯房地产业85%的资金来自民间资本，尤其是高利贷。开发商敢于借高利贷造房子是因为房价一直在上涨。<br />
<br />
2005年，当地民用住宅均价为每平方米1200元。2006年涨至1800元，2007年飙升到3000元，东胜区房价逼近5000元。<br />
<br />
而到了2008年，受金融危机影响，鄂尔多斯赖以生存的煤炭价格暴跌，房地产业失去了资金助推。同时由于宏观调控和需求释放等因素，鄂尔多斯楼市开始下行。<br />
<br />
彼时的魏东推销呼和浩特的楼盘必去两个省内城市：包头和鄂尔多斯。鄂尔多斯的投资客是许多楼盘的重点营销对象。但2008年再去推销的困难已是越来越大，鄂尔多斯人买房兴趣骤减。<br />
<br />
2009年初至今，楼市经历从回暖到再一次的快速增长。而鄂尔多斯因煤而富的程度可想而知。当地民间流传说，鄂尔多斯身家过亿的富翁有1700多位，其中三分之二来自于煤炭产业，三分之一来自房地产。而房地产业的原始资本积累也往往来自于煤炭。<br />
<br />
一时间，“鄂尔多斯炒房团”的名声已与温州炒房团并驾齐驱。习惯上，鄂尔多斯人先是在本地和呼市置业，再往北京和海南发展。<br />
<br />
魏东2009年就接触过鄂尔多斯的炒房团：一个团队十几个人，一掷千金会买下整栋楼，比如北京的数家酒店就是这样被买下改造成了酒店。<br />
<br />
魏东做出最坏的判断说，如果鄂尔多斯的煤炭资源枯竭了，房地产赖以为继的资金量发生变化，鄂尔多斯房地产市场才会真正难以为继。“当然海南也曾经存在泡沫，也破裂过，但是还是复苏了。”<br />
<br />
而事实是，鄂尔多斯煤炭探明储量1676亿吨，占全国储量的六分之一。如果不考虑煤炭需求的增减变化，即便粗略的按全市2009年约3.38亿吨的煤炭总产量算，鄂尔多斯的煤都够挖约500年。<br />
<br />
<br />
<br />
(本文来源：东北网 )</div>

							
							
							
							
