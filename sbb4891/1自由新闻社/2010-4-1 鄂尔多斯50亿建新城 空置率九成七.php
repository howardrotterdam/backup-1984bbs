鄂尔多斯市中的雕塑[编辑] 经济发展<br />
从1994年开始鄂尔多斯的经济规模强劲扩张,整个九五期间国民生产总是以年均20%的速度飞增,增长速度连续7年居内蒙古之首,9年内经济总量翻三番. 2004年鄂尔多斯市人均固定资产投资规模、人均国民生产总值增长额、羊绒、煤炭产量均位于全国城市排名第一。以其惊人的发展速度和效益形成“鄂尔多斯现象”。 鄂尔多斯GDP总量由2000年的150亿元攀升到2005年的550亿元。到2010年，经济总超过800亿，财政收入80多亿，城镇居民人均收入接近2万元。<br />
