													<h2>各地公务员超低价买房 变相福利分房已成公开秘密</h2>
						
						
																											<div id="postmessage_561744" class="t_msgfont">来源：央视网 2010年04月13日<br />
央视《新闻1+1》2010年4月12日播出：最后的“晚餐”，还要吃多久？以下为节目实录<br />
<br />
　　主持人(李小萌)：现在对于每个人来讲，房子都成了一件非常闹心的事。而对于那些荷包并不满的人来说，很大的希望就寄托在政府推出的保障性住房，比如说经济适用房上。为什么呢？因为便宜。但是接下来，您要看到的这些很便宜，地段又相当好的房子，恐怕和这部分人群是没有关系的。<br />
<br />
　　在眼下火热的房地产市场，一些政府部门变相的福利分房已成为公开的秘密。住房和城乡建设部政策研究中心主任陈淮对此的形容是公务员住房的“超国民待遇”。<br />
<br />
　　北京六里桥地区靠近南三环，毗邻北京西站和长途客运站，交通便利。周边的房价从去年的2万飙升至如今的3万左右。在这里，记者发现了一个特别的小区。当问到这个去年才建好的小区价格如何时，得到的回答会令很多人大吃一惊。<br />
<br />
　　居民：跟给的是一样的。<br />
<br />
　　记者：有这么便宜？<br />
<br />
　　居民：五千块钱一平米，不跟给的一样？<br />
<br />
　　记者：他们的房子是五千块钱一平米？<br />
<br />
　　居民：对。<br />
<br />
　　记者：周围的房子得几万块钱？<br />
<br />
　　居民：两万八吧。<br />
<br />
　　房屋中介公司人员：公务员够标准四千多元，额外的不够标准的就八千多元了，很多房子都是这么买的。购买80平米，房子90平米的，80平米按照四千元(每平米)买，额外的按八千元(每平米)买。<br />
<br />
　　记者：房子买的时候多少钱？<br />
<br />
　　居民：好像不一定有卖的，要求挺严格，不允许外卖。<br />
<br />
　　记者：不允许外卖？<br />
<br />
　　居民：福利分房。<br />
<br />
　　主持人：小区的居民说，在这个小区居住的公务员是以低于周边商品房数倍的价格买下的房子。虽然名义上不能卖也不能租，可是记者从小区门口的这家房屋中介公司得知，要租到这里的房子并不难，一套90平米的房子租金是3000多元。<br />
<br />
　　同样在三环边的黄金地带，同样是去年刚盖好，北京东三环也有这样一个特别的小区，这里居民去年的购房价比周边房价便宜了一大半。<br />
<br />
　　居民：当然便宜，比商品房便宜。<br />
<br />
　　记者：能便宜多少？<br />
<br />
　　居民：那便宜不少。<br />
<br />
　　记者：买的时候多少钱？<br />
<br />
　　居民：按一平米不到一万块钱，几千块钱。<br />
<br />
　　记者：现在附近的房价都得多少钱？<br />
<br />
　　居民：这边贵。<br />
<br />
　　记者：这边贵，将近三万？<br />
<br />
　　居民：两万多左右。<br />
<br />
　　主持人：在房产中介人员眼中，这个小区的房子是重点推荐的好房源。对客户介绍时，他们会格外强调是国家公务员住的房子，户型好、质量高，而且物业费单位出。<br />
<br />
　　房产中介人员：保安、绿化都好一点，环境好一点。前面这个楼多好，位置也不错，交通也方便，而且全是落地窗，房屋档次在这儿。<br />
<br />
　　房产中介人员：房子卖是不能卖的，要租的话，比其它小区还要贵的。<br />
<br />
　　记者：它不能卖吗？<br />
<br />
　　房产中介人员：它是央产房。很多人看那个小区，他说我三万一平米，你给我找一套我就买。<br />
<br />
　　记者：那不能想想办法？<br />
<br />
　　房产中介人员：他单位把这套房子直接分给他，然后他交钱，把这个房子他自己买下来之后，产权属于自己的时候才可以卖。<br />
<br />
　　记者：就是再等几年还可以买？<br />
<br />
　　房产中介人员：对。<br />
<br />
　　解说：虽然房子几年后才能出售，但现在已经有客户表示愿意花高价钱买下这个小区的房子；虽然房子购买时价格远低于市场价，但租赁价格却要高于市场价。如此特立独行的房子让不少人羡慕之极。黄金地段价格低廉，映衬在炙热的房地产市场中的确让人怦然心动，却又求之不得。而根据不少媒体的报道，如此特殊的房子全国各地都可以找到它们的身影。<br />
<br />
　　孙晓青(《小康》杂志记者)<br />
<br />
　　我走访的北京的一些公务员小区，大概分布在二环路比较近的地方，地段都非常好。一个是位于宣武区马连道茶城商业圈内，基础设施也非常好，这里的房价当时是4500元/平米左右，然后超出80平米的部分浮动10%。马连道当初周边的房价，当时大概在15000元/平米左右。<br />
<br />
　　解说：“好房卖官员”，这是媒体对去年在全国引起强烈反响的“温州购房门”事件的描述。去年3月，温州就发生过143套拆迁安置房变身成官员住宅的新闻。购房人中有市人大、市政府、市政协，房管局等机关部门的数十名市、县级干部。而这些安置房大多座落在温州繁华地段，有的房子市场价要卖到每平方米4万元，而当时的评估价却只有1万元，是市场价的四分之一。<br />
<br />
　　无独有偶，“温州购房门”事件刚被查处，几个月后，去年12月有媒体又报道：咸阳以每平方米2800元价格，卖给公务员2600多套房子，低于此地段房价数百元。<br />
<br />
　　这些超低价的好房子究竟是以什么名义诞生？我们似乎看得有些眼花缭乱，有的被叫做“经济适用房”，有的被说成是单位的“集资建房”。但共同之处在于，它们都是普通老百姓无法企及只能奢望的高档品，老百姓就算挤破了头也买不到。<br />
<br />
　　主持人：看过刚才的短片也许有人会说，这不奇怪，如果没有房子作为附加条件的话，那每年招考公务员的时候，怎么会有几千个人抢一个职位这样的现象呢？岩松，怎么看？<br />
<br />
　　白岩松：政府自己都不同意这种看法。为什么这么说？在1998年的时候开始实行房改，然后就停止了福利分房，中间还有一段过渡期，可以集资建房等等。2003年开始明确规范集资建房，到2006年时，就画了一条红线，党政机关都不许集资建房了。因此这样的行为是违规的。这是政府自身出台的相关规定，所以大家这样想其实是一个错误的想法。<br />
<br />
　　主持人：另外大家就要问一个为什么。为什么这些房子可以在这么好的地段，而且这么便宜？<br />
<br />
　　白岩松：我觉得这个时候因素可能就复杂了。第一，不排除如果公务员要靠他的收入，在逐渐高涨的房价当中买到一套住房，几乎是不可能的。但这不是理由。我们现在去分析公务员在整个社会上的收入水准，我们不能说他是中等偏上，起码是在中等这样一个范畴之内。同样中等收入的人群不也面临着同样的挑战吗？怎么公务员就能获取这种超高的国民待遇呢？这是第一个问题。因此收入不是问题，还有比他收入低很多的；第二个很重要的因素是，公权力可以获取比市场价格低得多的土地。大家都知道，现在的房价当中其实土地占走了太多的价格，因此利用我是党政机关甚至国家部委这样一个公权力，可以获取很便宜的地，那么房子自然就便宜了，因此职工就获取了利益，但是这里当然蕴藏着不公平；第三个，在单位内部，其实单位领导也面对着两个压力。他不光要向上看，还要向下看，因为要民主评议，底下也在给他施加压力，所以他也希望内部有一个和谐的气氛。因此，集资建房就在这样一个模糊地带逐渐成长。更何况这几年虽然画了红线，也没见谁“修理”过集资建房。<br />
<br />
　　主持人：就是在一个单位内部，不管是上还是下，干这件事都是有动力或者是有压力的？<br />
<br />
　　白岩松：当然，无利不起早也用于此。<br />
<br />
　　主持人：如果我们打一个比方说，划拨给建设保障性住房的土地是一块蛋糕的话，那么像我们刚才看到这类房子究竟占到了这个蛋糕多大的比例？我们来听听任志强的说法。<br />
<br />
　　任志强：<br />
<br />
　　我们从北京市2005年到2008年这几年时间土地供应情况做一个调查。公开的土地供应量，也就是面向社会的商品房只占百分之二十八点几，也就是说百分之七十以上的土地用于建经济适用住房和享受经济适用住房等政策的住房。那么其中我们也查了一下，从2003到2008年，面向社会供应的经济适用房的全部数量，加起来大概只有百分之七左右，连百分之十都不到。也就是说，剩下有百分之六十多的土地是用于“三定”，或者其它类似的房屋。<br />
<br />
　　主持人：任总甩出了几个数字，确实是挺令人吃惊的。<br />
<br />
　　白岩松：这是任总的观察。外行看热闹，内行看门道。假如任总说的是事实的话，你就要怀疑这百分之几十的土地都转移到哪去了？是不是相当多的地就作为有关机关或者部委来解决集资建房，或者定向给自己的职工作福利了？如果是这样的话，这就又变成一个新的问题，它涉及到公平问题。如果这百分之几十的土地投放到市场大环境下，是不是有助于平抑房价呢？是不是可以让更多的人去分享这个土地的蛋糕呢？但是没有，它几乎是悄悄地就变成了一种福利，重新回到一些公务员的住房当中。<br />
<br />
　　就像这期节目的标题一样---最后的“晚餐”还要吃多久？我们回头去看，实际上，就是在1998年那次房改开始的时候，最流行的一句话是“最后的晚餐”，赶紧抓紧，趁这个政策还允许的时候，公务员先把自己的住房解决了。没想到这晚餐一吃10年还拐弯了，10年到2008年，然后再拐弯到了2010年。于是人们就开始担心公平问题。你会突然发现，这几个月，大家炒得热热闹闹的房价问题，原来它已经不仅仅只是一个市场经济环境下的价格问题，它已经不是经济问题了，不是价格问题了，而是社会环境下的公平问题，恐怕要从解决社会环境下的公平问题来解决住房问题。<br />
<br />
　　主持人：对于出现了这么大比重的土地已经用于机关政府盖房，那还有没有办法把它追回来？<br />
<br />
　　白岩松：我觉得如果仅仅要用语言说的话，不一定起什么作用。可能要划一条线。既然是国土资源部、国务院出台的文件，在2006年明文禁止不许再集资建房了，那之前的我们也许管不着了，但是2006年之后的恐怕有关部门要严格处理，因为国家的规定明确地放在这儿。<br />
<br />
　　主持人：房子涉及到的已经不仅仅是经济问题，而且更是公平的问题。现在如果把“公务员”和“房子”两个关键词加在一起，你搜索一下就会发现，新闻还真不少。<br />
<br />
　　陈新胜(合肥市“清房办”负责人)：<br />
<br />
　　这次清查当中，个别公职人员买了或者租住了两到三套直管公房。我们该收回来的收回来，该补款的全部补款到位。我们合肥市的情况，最高的补了33.7万。<br />
<br />
　　解说：一间房就为国家追回了33.7万元，从追回国有资产流失的角度，合肥这次“清房行动”的效果不亚于查腐败。日前，针对违规占用公房的现象，合肥市出了一个狠招，展开了一次“清房行动”，让违规住户上缴房屋或补齐房款。<br />
<br />
　　陈新胜(合肥市“清房办”负责人)：<br />
<br />
　　合肥市这次清查，通过调查核实，房屋总共套数是14746套，违规户数是1226户。涉及到省市，包括中央驻合肥单位200多个。我们现在已经纠正了1119户，补款5000多万元，退房30套。<br />
<br />
　　解说：让人震惊的是，在已经退回的这30套公房中，违规者中竟然有一半都是党政机关和企事业单位的公职人员。劳动局、公安局、民政局、国土局、老干部局、计生委、法院、邮政局、房地产管理局等等政府机关和事业单位，都有工作人员在违规之列。有个别公职人员手上包括房改房已经有数套房子，竟然还捏着一套直管公房。<br />
<br />
　　陈新胜(合肥市“清房办”负责人)：<br />
<br />
　　凡是党政机关、事业单位的公职人员，拒不纠正和阳奉阴违的，一经查实，坚决辞退。<br />
<br />
　　解说：多占公房的公务员韩某，赶在自查自救的截至日期之前来退房。<br />
<br />
　　记者：该不该退？<br />
<br />
　　韩女士(合肥市公务员)：<br />
<br />
　　退还是应该退的。已经享受了国家的福利房，我作为一名国家机关工作人员，清房工作本身就是一块比较难做的工作，应该积极配合政府。<br />
<br />
　　解说：公平与公正，如今确实成了公共福利房资源所面临的最大困境。巨大的利益驱使着部分掌握特权的人，千方百计地占有公共福利资源，经适房、廉租房、安置房都成了他们的目标。也正是他们手中的特权，让清理工作困难重重。在西部的一个省会城市，曾经由于重重阻挠，一年中，全市居然只清理出了一套违规侵占的公房。难怪在这次合肥 “清房行动”前，合肥市委书记孙金龙做了这样坚决的表态。<br />
<br />
　　孙金龙(合肥市市委书记)：<br />
<br />
　　没有特权单位，也没有特权阶层。无论大小多少，不管涉及到哪一级部门，不管事关什么背景的人，都要敢于碰硬，严格执法。<br />
<br />
　　解说：这样铁腕清房的姿态无疑取得了不错的效果。对于查处出来的公职人员，“清房办”也本着敢于碰硬，一视同仁的态度进行了查办。<br />
<br />
　　雍成瀚(中共合肥市纪律检查委员会书记)：<br />
<br />
　　在我们清房面前，在我们维护法律、维护制度、维护纪律的尊严面前，应该说没有特权单位，没有特殊的个人。<br />
<br />
　　解说：像合肥这样的侵占公共福利房资源的现象，在其它城市同样存在。深圳在2007年的时候，也曾经对公务员违规占用廉租房进行过清查。<br />
<br />
　　深圳市住宅开发修缮租贸中心工作人员：退房的，现在有一百多人了。<br />
<br />
　　解说：像这样去现场办退租的公务员络绎不绝，足以见得违规人数众多。他们几乎都是在分到福利房或者买到房之后，并没有退还廉租房，而是继续占用甚至转租获利。今天，深圳另外一则关于公务员申请保障房的后续新闻就让人有此感受。据相关负责人介绍，迫于压力，已经有个别公务员申请人因条件不够主动退出公示。上周五《新闻1+1》节目中播出的齐齐哈尔驻深圳办主任石某，因为怕影响在法国读书的孩子，也已经退出了申请。<br />
<br />
　　当特权遇上利益，该如何保证公正与公平？公共福利资源又该如何分配给真正需要的人？这也许是摆在全社会面前的一道考题。<br />
<br />
　　主持人：在这儿我们要把几种房子的性质稍微理清一点。合肥清退的属于直管公房，它是不同级别的房屋管理部门所管理的房子，产权属于国家。像这样的清退工作，其实它执行起来的力度和效果，应该和之前我们所说的经济适用房那种情况，应该是不一样的？<br />
<br />
　　白岩松：它们有相同的地方。一些部委集资建房和占用公房有一点是相同的，都涉及到公平问题，都会让社会上，比如说正在为住房和蜗居感到窝心的人感觉更加火冒三丈。另一方面又有不同的地方。因为后者明确地属于个人侵占国家财产，已经拿国家的财产来获取自己的利益。也就是说，明明是违规的最后“晚餐”，已经变成了自己的“一日三餐”，因为他手里拥有好几套，可以租房，甚至变成了门面房。当然这里有一些因素，比如说，有的单位已经没了，这房子就变成他个人的了。还有，当初在分房的时候，那里的一些花账，一些违规的地方导致这种现象出现。所以我觉得，要为合肥以及一些城市在明确做这样清房行动大声喝彩。因为目前大家关注住房问题的时候，不光是房价的问题，我甚至认为，公平的问题已经超过房价问题。<br />
<br />
　　主持人：但也有人替公务员说话，对公务员也有一个公平性问题。因为他们的工资在社会当中来看不算高的，让他们自己买商品房也不行，他该拿到类似福利性质的房子？<br />
<br />
　　白岩松：带有一定的保障性的住房，是全世界对于公务员的一个通行的规则。但是，第一，我们要先划一条底线，他不可能超国民待遇，就是说你以不公平的方式去获取资源，这不可能。比如说很多国家采用给予一定补贴的方式，包括类似于我们公积金的方式，其实这样的方式原本不就是在解决公务员住房的问题吗？第二，我觉得要很清楚，不是一当公务员就可以有住房。也跟社会上其他人群一样，到达一定年龄，有了一定的积累，你才可以由小面积慢慢地变大。在这方面，包括公积金、包括经济适用房，包括一定的租赁房屋，都是解决公务员住房的一条路，但是前提是必须公平，第二还需要透明。<br />
<br />
　　主持人：现在房子对于每个人来讲都太敏感了，房子的问题当中又出现了公职人员，出现了不公平。<br />
<br />
　　白岩松：我觉得这句话非常非常重要。<br />
<br />
　　主持人：它的危害性应该不仅仅在于房子本身？<br />
<br />
　　白岩松：没错。我刚才说本来已经“火冒一丈”了，如果要不解决，他会火冒三丈。但是我们看到了，像合肥等等很多地方，已经开始在这儿下刀子，而且刀很快，这是一个好的开始。 另一方面，的确有这样一种担心，如果国家机关、政府等等都采用集资建房的方式，他自己的住房不愁，你觉得他会有和所有面对房价心急如焚的普通百姓一样的担心，真正有动力去治理房价问题吗？另一方面，解决住房的问题当然还要靠政府。因为很重要的一点在土地这儿。当我们要去规范市场，要规范不法的企业家、房地产商，但是最重要的一点，政府要从自身做起。<br />
<br />
　　主持人：很多环节都是政府在做的。政府在主导、政府在分配，政府部门的人在享受福利待遇，那怎么在程序上做到公平和正义呢？<br />
<br />
　　白岩松：首先我们要看到政府已经非常明确了。从《政府工作报告》来说，从中央政府来说，为了使房地产市场能够规范、健康、公平的发展，可以说是一道金牌又一道金牌。但是现在就需要真正的落实，解铃还需系铃人。</div>

							
							
							
							
