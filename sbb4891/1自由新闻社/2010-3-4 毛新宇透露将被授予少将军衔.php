																											<div id="postmessage_501408" class="t_msgfont">毛新宇透露7月将升少将 推崇毛泽东与拿破仑<br />
2010-03-04 04:41:22　来源: 新民网　跟贴 32 条 手机看新闻 <br />
核心提示：全国政协委员毛新宇接受记者采访时表示7月将被授予少将军衔，并称比较关注教育、代课老师、医疗等社会民生问题，和妻子是介绍认识，但属于自由恋爱，除了毛泽东外比较推崇拿破仑<br />
<br />
<br />
 全国政协委员毛新宇在听政协大会报告。新华社发<br />
<br />
新民网3月4日报道 前日下午，毛泽东的唯一嫡孙、全国政协委员毛新宇接受记者采访时表示，今年两会他的一个提案是建议把毛泽东思想应用于未来的信息化战争中。<br />
<br />
除了毛泽东，推崇拿破仑<br />
<br />
自2008年初当选为全国政协委员后，毛泽东的唯一嫡孙、中国人民解放军军事科学院战争理论和战略研究部副部长毛新宇今年是第三次参加全国两会。他说：“我在前两年的提案中都没有关注军事方面的问题，而今年不一样，我所提的一个重要提案，就是用毛泽东军事思想来指导我们未来信息化战争。”<br />
<br />
毛新宇认为：“美国的学者都很重视中国军队的发展，他们不怕我们军队的现代化，就怕我们的军队毛泽东思想化。”<br />
<br />
当被问及“除了毛泽东之外，最喜欢哪个军事家”时，毛新宇表示他比较推崇拿破仑。“如果说单单把拿破仑评价为一个军事家，还是片面的。其实拿破仑也可以基本上算一个成功的政治家。因为拿破仑对世界的影响，除了辉煌的战役胜利以外，还制定了一部法典，叫《拿破仑法典》，它现在对于欧美国家仍然产生着重要的影响。另外拿破仑作为一个杰出的政治家，他摧毁了法国的封建制度。”<br />
<br />
关注农民工　7月将晋少将<br />
<br />
除了军事方面，毛新宇还比较关注教育、代课老师、医疗等社会民生问题。他赞扬在广州和深圳了解到的一个政策：农民工在他所打工的城市，他的子女在他所打工的城市上学都是免费的。<br />
<br />
在去年，毛新宇因被传成为全国最年轻的少将而被网民热议，各大媒体也对此纷纷报道。当记者向其问及此事时，毛新宇说：“现在都还不是，今年7月份，上级才将正式向我授予这一军衔。我很感谢全国人民对我的厚爱，大家是把对我爷爷的爱转移到了我身上。其实我个人不在乎军衔级别的高低，我只希望能在现 在 的 工 作 岗 位 上 多 做 出 成绩。”<br />
<br />
“我们也是属于自由恋爱”<br />
<br />
“你和你爱人是怎么认识的?是自由恋爱吗?”面对记者的提问，毛新宇微微一笑说：“我和她一开始是经别人介绍认识的，但我们也是属于自由恋爱，这是一种缘分，我们现在的生活很幸福、快乐，谢谢你们的关心。”<br />
<br />
毛新宇在访谈中坦言，目前自己的孩子占据了他生命很重要的部分。“除了搞课题，除了锻炼以外，我一般在家里，就把两个小孩叫到一起。小时候我母亲就是这样教我的，我给他们讲成语故事。有时候教他们写几个字，看他们画画。有的时候我儿子还问我一些英语单词，也挺有意思的家庭生活。”<br />
<br />
