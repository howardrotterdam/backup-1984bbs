													<h2>车轮滚滚，使命在肩――访《归途列车》导演范立欣</h2>
						
						
																											<div id="postmessage_802672" class="t_msgfont">车轮滚滚，使命在肩――访《归途列车》导演范立欣<br />
2010年01月29日 星期五 下午 04:43<br />
<br />
范立欣导演在EyeSteelFilm制片公司接受采访 摄影：胡宪<br />
<br />
2009年11月11日，蒙特利尔第十二届国际纪录片电影节隆重开幕，旅加中国导演范立欣的处女作《归途列车》（Last train home）作为开幕影片获得极大成功。<br />
<br />
11月12日，范立欣导演在EyeSteelFilm制片公司接受了七天记者的独家专访。<br />
<br />
从电视人到电影人<br />
<br />
影片上映后，当地媒体争相报道，中西观众交口称赞。对范立欣本人来说，成功还谈不上，最令他欣慰的是片子达到了他的预期目的：“让每一个人看过后都停下来，想一想我们今天身处的世界。”<br />
<br />
范立欣生长在中国湖北省武汉市，21岁毕业于华中理工大学汉口分院英文系，从98年起先后在武汉电视台和中央电视台任新闻记者，06年移民加拿大，加入以现实反映全球社会政治问题为己任的纪录片摄制公司EyeSteelFilm。2006年，范立欣在中国协助拍摄纪录片《沿江而上》（Up The Yangtze），而《归途列车》是他导演的第一部纪录长片。<br />
<br />
央视记者，中国的“无冕之王”，是什么让他改行做了电影人呢？范立欣说：“动力可以说来自两个方面。在电视台工作，经常去农村出差。每当从贫穷的农村飞回到繁华的城市，总是强烈地感受到中国城乡贫富差距实在太大了，大到我无法接受。特别是那些农村青年对我讲述他们的希望与憧憬，总让我觉得应该为他们做些什么。另一方面则是受家庭教育的影响。我出生在一个传统家庭，父亲当过兵，又教过书，我母亲是会计师。从小时候开始，父母就教育我们要与人为善，要有独立的见解，要为社会做出应有的贡献。” 2003年，范立欣和武汉电视台陈伟军导演共同制作了反映中国艾滋病家庭的纪录片《好死不如赖活着》，引起极大反响。在这之后，他意识到通过纪录片这种媒介，每个人都可以独立思考、自由发言，更加直接地参与和影响社会的进程。之后，范立欣来到中央电视台做了一名摄影师兼记者。在工作之余，他仍热衷于纪录片的创作，不断积累专业知识和技巧。<br />
<br />
如今，范立欣终于如愿以偿成为一名名副其实的纪录片电影人。<br />
<br />
农民工情结<br />
<br />
范立欣说：“在中国改革开放，发展外向型经济的过程中，牺牲最大、贡献最多的就是农民工，是农民工的辛苦换来了城市和国民收入的迅猛发展和傲人成就，遗憾的是城市人意识不到这点。生活在城市里的人往往片面地认为农民工给城市带来了脏乱差。他们见到农民就赶紧躲开，生怕沾上臭味。每当我看到这样的事情发生，心中无不遗憾，甚至气愤。”<br />
<br />
“你没有权利、没有道理这样做！”范立欣总想对那些忽视农民工兄弟的人这样喊。由于政策的不健全，农民工的衣食住行得不到保障，孩子上不了学，生病没钱治……中国人口多，资源少，面临各种挑战，从政府到老百姓应该怎样做？这些因素都促使范导演从02年就开始思考农民工的问题。“我希望拍摄一部反映农民工生活的纪录片，引发社会各层对他们关注。”<br />
<br />
影片《归途列车》以四川农民工老张夫妇07、08、09连续三年为回家过年历尽艰辛为主线，以贴身纪实的手法，再现了中国农民工许多辛酸的两难处境，以及由此衍生的各种社会问题。片中农民工那些真实的挣扎、哭喊、厮打与妥协，振聋发聩，令人深省。有观众说，看了这部影片才了解到中国问题的复杂性，解决起来绝不像原先想象的那么简单；有华人朋友虽然来自中国，却从来没有想过农民兄弟的疾苦，看了影片后，在不安和惭愧的同时，对中国的现状与未来也有了更深刻的思索。<br />
<br />
范立欣说，过去三年的拍摄过程，也是他自己的成长过程，有些深层次的矛盾他在最初选题时也没有料到，例如，由于父母进城打工，把孩子交给祖父母看管，由此带来的亲子隔膜、传统家庭模式和道德观念的破裂；更为重要的是，这个农民家庭的故事，几乎折射出中国现代所有的尖锐矛盾，例如社会公平、劳动法规、留守儿童和老人的前途命运等等。与此同时，片子还反映了在当今全球化的浪潮中，中国作为一个制造大国、“世界工厂”，在全球经济链条中所面临的挑战。<br />
<br />
这也许就是《归途列车》之所以产生了轰动效应的原因，在中国的经济腾飞令全球震惊的今天，这部影片让世界看到了更加真实全面的中国。<br />
<br />
信任是成功关键<br />
<br />
从片中主人公洗脚睡觉的生活细节，到父女动手打架的激烈场面，可以看出张昌华一家对摄制组的信任。范导说他们的关系到后来就像一家人一样。片中有一组镜头令人震撼：张家女儿张琴在和父亲的对打中对着镜头大喊：拍吧！拍吧！现在你们看到了，这才是真正的我！<br />
<br />
人们都知道，拍摄记录片不能使钱，不能导演，一切镜头都必须出于自然。那么，范立欣是如何做到的呢？<br />
<br />
他说：“06年12月我第一次去农民工最密集的广州市搞研究，拍了许多的工厂和作坊，挨门挨户地跑，一个接一个地找民工聊。在那种鱼龙混杂的地方，人与人之间很难建立起信任，刚开始有些民工兄弟，工厂老板还把我当骗子，轰我走。大约走访了一个月以后，终于找到比较有代表性的老张夫妇，说服他们参与拍摄这部纪录片。”范立欣说：“张哥和陈姐虽然都只有小学文化程度，但他们身上所表现出的中国农民淳朴、善良的美德，让我非常感动，也受益良多。”<br />
<br />
在接下来的三年里，范立欣和他的摄制组成员就再也没在自家过过春节，他们每年都和老张一家“抢购”火车票，挤上火车，一起过年，曾五次往返于广东和四川拍片，后来老张的女儿去深圳打工，他们又追踪去了深圳。<br />
<br />
08年春运期间，由于冰灾影响，摄制组和千千万万的农民工一样被困在广州火车站，好几天才等来一辆火车。他和摄制组成员不光要和农民工一样疯狂地“爬摸滚打”，还要背着摄影机去捕捉那些最真实，最感人的镜头。<br />
<br />
对于在城市里养尊处优长大的一代，可以和农民同吃同住，已经颇为不易，能够和农民交心，并取得他们的信任，这不仅需要真诚，还需要一定的社会阅历和相当的政治文化底蕴。<br />
<br />
中国年轻导演范立欣，以《归途列车》向人们证实他具有这些素质和实力。<br />
<br />
“列车”继续向前<br />
<br />
看过影片的人普遍有一个感觉，范立欣没有去刻意迎合某一个社会集团的利益或口味，美化或丑化今天的中国，他凭着一个纪录片影人的良心和一个年轻人对社会应有的责任心，客观地把中国社会存在的种种矛盾以缩影的形式向世界展现。范立欣认为，今天的中国离不开世界，今天的世界也离不开中国，他说：“我希望自己能为东西方交流做点力所能及的贡献。我大学上的英语专业，现有机会在加拿大拍中国题材的电影，我应该承担起自己的责任。”他还表示将来也会拍一些加拿大或美国题材的纪录片，让中国更加了解西方社会。<br />
<br />
范立欣更喜欢用“交流”这个词去替代“宣传”，他认为“宣传”有强加于人的感觉，而“交流”，则是平和的、双向的。他说：“中国很需要找到一个正确的方式，或者说用‘世界’能理解的方式，使‘世界’愿意坐下来听你讲，跟你交流。我希望能用自己的一些优势，在东西方交流中当一架桥梁。”<br />
<br />
上个月，范立欣又回了趟中国，他去了甘肃。中国政府计划在广袤西部的“丝绸之路”上修建一座“陆上三峡”――世界上最大的风力发电站。范立欣要用自己的“语言”向世界讲述中国是如何致力于绿色可持续性能源推广的；在强调构建和谐社会的同时，是不是也注重了人与自然的和谐？<br />
<br />
除了环保，范立欣将继续把目光盯在农民工身上。“经济危机导致了大批农民工下岗，已经习惯了城市生活方式的他们，出路何在？成千上万的年轻人错过了受教育的最佳阶段，国家有没有相应政策使他们接受再教育？这么一大批没有受过教育，又回不到田里的年轻人，他们的未来是什么……”范立欣深深感到他的历史责任就是继续关注和了解他们。<br />
<br />
范立欣的《归途列车》不像有些影片那样，只能“园内开花园外香”，他的影片在中国内外都受到了重视和欢迎。该片从11月20日起，在蒙特利尔AMC和Cinema Parallel两座影院公映。该片还入选了世界上最著名的阿姆斯特丹国际纪录片电影节的最高竞赛单元――伊文思奖竞赛。最让范导高兴的还要算《归途列车》入选了今年广州纪录片大会。“我想这是我们的国家和政府在不断改变，不断进步的一个重要标志，”范导说：“这个片子在广州拍了三年，今天能回到广州作为中国首映，我们算是‘回家过年’了。”<br />
<br />
范导还说他会在电影节公映前和摄制组一起去和张昌华夫妇见面，给他们看这部片子，感谢他们为全国农民工兄弟做出的努力和贡献。<br />
<br />
11月21日，好消息传来，范立欣的《归途列车》荣获2009蒙特利尔国际纪录片电影节“加拿大魁北克最佳影片”大奖。<br />
<br />
这个大奖是“列车”胜利靠站的荣誉，更是即将启程的汽笛。在东西方交流日益加强的今天，在纪录片创作不断深化的道路上，让我们跟随范立欣导演的“使命列车”继续向前。</div>

							
							
							
							
