													<h2>一对农民工夫妇的春节回家路</h2>
						
						
																											<div id="postmessage_477831" class="t_msgfont">2010年 02月 11日 11:18<br />
一对农民工夫妇的春节回家路<br />
<br />
<img src="http://imgur.com/nnRji.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
中国春节近在眼前，中国8.6万公里的铁路沿线又一次被来自全国各地的人们挤得满满当当，大家都要赶回家过这个一年中最重要的节日。<br />
<br />
中国国家发展与改革委员会（简称：发改委）预计，在1月30日至3月10日的春运高峰期间将总计发送旅客25.4亿人次，差不多相当于每个中国人往返一趟。中国春运也号称全球最大规模的年度迁移行动。铁路运送旅客预计达2.1亿人，其中绝大多数是来自农村地区的外出务工者，回家与家人进行一年一度的团聚。<br />
<br />
一对40岁出头的农民工夫妇张昌华和陈素琴是纪录片《归途列车》(Last Train Home)记录的对象。这部纪录片去年年底获得最佳纪录长片VPRO IDFA奖。<br />
<br />
这部纪录片由加拿大籍华人、独立电影制作人范立欣制作，讲述了张昌华夫妇在2007-2009年间的三个春节从打工地广州回到1,900公里外的四川老家的艰难旅程（一路上乘火车、坐船、坐汽车）。夫妇二人的故事反映了中国1.5亿外出务工者的阴郁现实。这对夫妇16年前去广州工厂打工，将当时才两个月大的女儿留在家里交给奶奶照看。<br />
<br />
多年来，夫妻俩在制衣厂辛苦打工，供女儿张琴和小儿子张扬读书。然而张琴却与父母日渐生疏，也越来越叛逆。她于2007年退学去了广州，跟父母一样成了外出务工者的一员。<br />
<br />
这部纪录片的叙事在2008年春节达到高潮，当时张昌华夫妇想带女儿回家，劝她回去上学。由于那年华南大部分地区暴雪，三人与另外60万名旅客一起被困在广州火车站长达5天。<br />
<br />
然而，父母的努力最终换来了女儿的激烈反抗，十几岁的张琴当时已在广州一家做外贸牛仔裤的工厂打了半年工，她朝父亲大吼，发誓永远不会回去上学。在纪录片末尾，2009年全球金融危机的影响已经波及广州省大多数工厂，陈素琴决定回老家照顾儿子张扬，当时张扬已经显示出厌学迹象。陈素琴让丈夫独自留在广州挣钱养家。<br />
<br />
陈素琴在被梯田围绕的自家屋里对儿子说，农民的孩子一定要努力读书，不然只能跟我们一样。<br />
<br />
该纪录片的导演范立欣出生于知识分子家庭，武汉人，曾在中国官方媒体中央电视台（CCTV）工作，2006年移居加拿大。他说在为中央电视台工作期间他经常去农村出差，亲眼目睹那里的贫困以及与都市生活的鲜明反差。他希望以此片向农民工表示敬意。在他看来，农民工才是今天中国社会繁荣的基石，却一直没有获得最基本的社会生活保障。<br />
<br />
该纪录片近两三个月一直在加拿大和美国公开上映，去年12月广州国际纪录片大会期间也曾对少数中国观众放映过，但该片尚未公布在中国大陆的上映计划。<br />
<br />
该纪录片获得约100万美元资金支持，主要来自加拿大和美国的金融和广播公司。该片在海外获得了质量高、具有时代精神的赞誉。但中国的一些观察人士对范立欣在中国拍摄如此令人不快的影片的动机表示质疑。<br />
<br />
范立欣辩解说，他之所以聚焦于中国巨大的农民工群体这一平凡的主题，是希望这部纪录片能够成为在发达国家过着优越生活的人们与在中国工厂里辛苦工作、为全世界的消费者生产产品的农民工之间理解的桥梁。<br />
<br />
影片中的母亲几个月前又回到工厂打工，因为她丈夫赚得钱不足以养家。<br />
<br />
