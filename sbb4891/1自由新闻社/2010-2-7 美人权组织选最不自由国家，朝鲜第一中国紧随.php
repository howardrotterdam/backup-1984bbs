<br />
从陈秋可大叔那里看到腾讯的一篇叫做《美国评选出全球最不自由9个国家 朝鲜排第一》的新闻，来源是新华社的文章。<br />
当然说是美国，其实只是美国的人权组织Freedom House （自由之家）每年的例行报告而已。文章列出了所谓的全球最不自由的9个国家，他们依次是：朝鲜、土库曼斯坦、索马里、苏丹、乌兹别克斯坦、缅甸、厄立特里亚、利比亚和赤道几内亚。<br />
但是问题是Freedom House的这个自由指数是只评分，不排名的，这个所谓的全球最不自由的9个国家纯粹是某些人意淫出来的，至于意淫什么，为什么要意淫，你自己想。<br />
　<br />
<br />
<br />
<br />
简单来说一下这个自由指数是怎么弄的，还有这个最不自由9个国家是怎么来的<br />
这个指数包含两个部分，一个是政治权利（PR），一个是公民自由(CL)。这两项评分都是从1分到7分，其中1分表示最自由，7分表示最不自由，然后取两者的平均分。而这最不自由的9个国家都是两项全部得到7分（最不自由），最后平均分都是7分的。<br />
有些人肯定就关心，贵国拿了多少分呢？按照今年的评分报告，贵国的政治权利(PR)一项是7分，公民自由(CL)一项是6分，平均分6.5分。<br />
那贵国历年拿了多少分呢，Freedom House的网站上有2002年到2009年的分数，分别是<br />
PR代表政治权利 CL代表公民自由<br />
2002：PR 7 CL 6<br />
2003：PR 7 CL 6<br />
2004：PR 7 CL 6<br />
2005：PR 7 CL 6<br />
2006：PR 7 CL 6<br />
2007：PR 7 CL 6<br />
2008：PR 7 CL 6<br />
2009：PR 7 CL 6<br />
