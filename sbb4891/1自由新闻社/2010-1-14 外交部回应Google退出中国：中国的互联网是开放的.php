维权人士Gmail纷遭入侵 北京称管制措施符合国际惯例 <br />
<br />
【明报专讯】Google声称因遭黑客袭击可能撤离中国大陆后，内地维权人士昨日纷纷透露自己Gmail账户遭入侵，而所收发的邮件会被传送至不明邮址。有维权律师直言，「中国是世界上互联网最不开放的地方」。中国外交部发言人姜瑜昨日强调，中国有关管理互联网措施符合国际通行的做法。<br />
<br />
记者昨日再将「六四事件」、「零八宪章」等敏感的政治字眼在谷歌（Google中国）搜寻，虽然网页上能找到有关标题，但却无法再搜寻到有关文章具体内容，情况跟前日差不多。<br />
<br />
英文《中国日报》引述谷歌一名不愿透露姓名的发言人表示，高层决定关闭的在华业务仅仅是Google.cn，Gphone和Gmail不会受到影响，网民仍可继续通过Google.com搜索中文内容。<br />
<br />
电邮被转发至不明地址<br />
<br />
姜瑜昨日在记者会上并没有正面回答中国政府对Google事件的看法，只表示「中国的互联网是开放的，中国政府鼓励互联网的发展，努力为互联网的健康发展营造良好的环境。」她还说，中国的法律禁止任何形式的黑客攻击。对于美国国务卿希拉里要求中国解释，她响应说「如果美方联系中方，我们将向美方重申这一立场。」<br />
<br />
维权律师滕彪则反驳，「这完全是赤裸裸的谎言，中国是世界上互联网最不开放的地方。」他对本报说，他检查自己电邮（<a href="mailto:tengbiao89@gmail.com">tengbiao89@gmail.com</a>）设定，发现被人更改过，所有收到的邮件都已被转发至另一个电邮「<a href="mailto:tengbiao1989@gmail.com">tengbiao1989@gmail.com</a>」，虽然名称类似但并非他所有。此外，维权律师江天勇、维权人士胡佳的妻子曾金燕等人也表示，他们的电邮被黑客动了手脚。<br />
<br />
教授：削弱监督贪腐官员<br />
<br />
而目前正在台湾的六四学生领袖王丹说，他和民运友人的电邮过去也遭侵入，「所以我们平均每月换1次密码」。他认为，如果Google退出中国大陆，会对中国政府限制互联网自由的作为产生一定压力。英国广播公司（BBC）报道，雅虎、Symantec及Adobe等公司昨日也声称自己受到黑客攻击。<br />
<br />
北京理工大学教授胡星斗认为，Google一旦离开，大陆互联网就进入寒冬。届时，大陆互联网将会成为一个巨大的封闭的局域网，而未来在反腐、贫富分化、官员特权等问题的监督亦会在一定程度上削弱。但北京大学教授贺卫方认为，「与其委屈求全，不如一走了之，Google作此决定不是坏事。」<br />
