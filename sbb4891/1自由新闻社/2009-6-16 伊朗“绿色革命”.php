													<h2>中国特色互联网管理不再独行</h2>
						
						
																											<div id="postmessage_126231" class="t_msgfont">新京报6月18日报道 17日，伊朗大选被指舞弊引发的政局动荡已经进入第4天，此前一天，大选败者、改革派穆萨维的支持者依然无视政府禁令举行抗议示威。17日，伊朗革命卫队发出强硬声明，宣布将限制网络媒体，严惩在网上“制造紧张局势者”。<br />
<br />
反对派继续游行示威<br />
<br />
16日，在穆萨维的支持者举行抗议示威的同时，在大选中获胜的现总统内贾德支持者也组织“团结造势集会”。当天，德黑兰市中心的阿斯尔广场成了内贾德支持者的海洋，他们指责穆萨维和西方试图破坏国家稳定。<br />
<br />
而穆萨维本人17日则公开呼吁支持者18日举行大型集会，为15日被军警开枪打死的学生和平民举行哀悼和纪念。<br />
<br />
军队已派“网络侦探”<br />
<br />
面对改革派继续发动示威的势头，直接受命于伊朗最高领袖的伊朗精英部队―――伊朗革命卫队17日发表声明，宣布将对网络媒体进行限制。由于伊朗政府16日已禁止外媒外出采访伊朗政局，网络成为改革派示威者报道消息和互相沟通的主要工具。<br />
<br />
这份声明称，伊朗的网站运营商和博客写手必须要移除那些“制造紧张局势”的网上内容，否则将面临“法律的严厉惩罚”，而革命卫队的调查者已经开始行动。<br />
<br />
声明还指责称，一些“反动网站”得到加拿大、美国、英国等外国势力的支持。<br />
<br />
不少穆萨维阵营的媒体对革命卫队介入选举表示不满。他们称革命卫队并不应该参与政治。<br />
<br />
最高领袖呼吁平息争端<br />
<br />
伊朗最高领袖哈梅内伊在选举结束后就没有公开露面，但选后一直不断表态。17日，他与总统大选4名候选人的代表举行了会谈，哈梅内伊要求所有各方不要对各自支持者发出错误的信号，更不要利用选举搅乱社会局势。“谁都不要加剧紧张局势，相反应该让它尽快平息。”<br />
<br />
哈梅内伊也要求区别对待选民、游行者和暴力事件制造者，“在选举中，选民有不同的主张，但是他们都共同地相信现存的制度，并支持伊斯兰共和国。游行群众并不想推翻共和国，暴力事件是反伊斯兰的敌人策划实施的。”<br />
<br />
哈梅内伊也重申，他支持在部分地区重新统计选票，但不赞同穆萨维一派全部重新统计的要求。<br />
<br />
■ 对垒<br />
<br />
政府:策划非法抗议或面临死刑<br />
<br />
继伊朗情报部长埃杰耶证实安全部队逮捕数十名涉嫌煽动示威活动的“主谋”后，伊朗一名地方官员17日警告非法抗议幕后策划者可能面临严惩。<br />
<br />
半官方的法尔斯新闻社援引伊斯法罕省总检察长哈比比的话警告说，大规模示威活动背后存在少数不法分子，他们受控于伊朗境外势力。根据伊斯兰法律，这些人最高可能面临死刑。哈比比警告这些人停止“犯罪活动”。<br />
<br />
“我们警告那些外国人控制的少数不法分子，试图通过煽动个人破坏和纵火等扰乱国家安全的行为将面临死刑，”哈比比说，“因此，在被绳之以法之前，他们应该回归祖国怀抱，避免再有犯罪行为。”<br />
<br />
另据路透社和法新社援引消息人士的话报道，支持穆萨维的改革派人士贾莱普尔和政治评论人士莱拉兹17日早晨相继遭到逮捕。其中，莱拉兹名下的报纸一直对现总统内贾德的经济等政策提出批评。<br />
<br />
反对派<br />
<br />
“网络串联”呼吁街头示威<br />
<br />
穆萨维的支持者多是年轻人和大学生，在大选中和选举争端产生后，不少人利用网络，互相在网上通报消息，鼓励走上街头。<br />
<br />
在伊朗著名博主加蒂的博客上，很多支持者发帖称，他们在德黑兰的加姆地区自发集会，声援穆萨维，集会名为“安静示威”，因为没有人呼喊口号，只是静静地行走或静坐。一位在加蒂博客上通报情况的伊朗人16日凌晨留言说：手机信号又被屏蔽了，人们用沉默来表达抗议。有人跟着留言称：“17日再见！”<br />
<br />
在另外一些社交网站上也出现了“17日再见”的消息，一些人还发表留言：如果不能在选举中获得合法权利，我们将考虑长期示威。<br />
<br />
加蒂本人发表博文说，很多博客发不了消息，所以很多人用代理服务器登录被屏蔽的网站。而政府为了防止这种现象，干脆断了不少地区的互联网。<br />
<br />
<strong>还有伊朗网友发帖称，在伊朗同韩国的一场足球赛中，一些队员也戴上支持代表穆萨维的绿色腕带。</strong> <br />
<br />
■ 连线<br />
<br />
驻伊朗中国记者采访遇困难<br />
<br />
中国国际广播电台驻伊朗记者罗来安17日表示，伊朗政府确实已经给各国记者发出了通知，要求他们不要采访“未经许可”的游行，否则相机会被没收，记者证会被吊销。<br />
<br />
罗来安对记者介绍，“这个禁令一出，新闻来源肯定受到影响，采访比较困难。”但他说，内贾德支持者16日的集会获得了政府许可，可以去采访；但是穆萨维等改革派支持者的集会通常不会获得许可，如果去采访的话就会“遇到麻烦”。<br />
<br />
电话那头的罗来安对伊朗政府这一规定表示无奈。“目前只能看伊朗政府的电视，不过里面的消息也不怎么报道改革派的情况。而最近上网人多，网速也很慢，手机信号很不稳定，短信在13日左右就被屏蔽了。”<br />
<br />
■ 观察<br />
<br />
街头抗议恐将持续<br />
<br />
无论重新计票的结果如何，社会分裂消弭难度大<br />
<br />
大选后的政治僵局在一定程度上折射出伊朗普选政治无论从形式上还是从内容上都“欠缺火候”。<br />
<br />
在大选尚在进行时，伊朗政府首次引入候选辩论这一新形式，但是辩论过程中却有不少“不正常现象”。比如内贾德成功挑起“腐败辩论”，却在没有拿出确凿事实的情况下，点名批评对手。<br />
<br />
通常，在没有证据的情况下攻击竞选对手，很容易授人以柄，不利选情。而伊朗媒体事后的民调却显示，内贾德“无端攻击”赢得超过半数选民的支持。<br />
<br />
而选举后的争论和暴力也折射出伊朗普选政治诸多问题。比如独立选举监督员的缺失、选票统计过快、部分网络被屏蔽、民众上街引发骚乱、两派互不宽容等。<br />
<br />
从目前的局势发展看，无论重新计票的结果如何，街头运动很难消弭，伊朗社会的裂痕也将难以短期弥合。<br />
<br />
如果宪法监护委员会确认内贾德依然获胜，穆萨维的支持者不会善罢甘休；如果宪法监护委员会推翻之前的结果，内贾德一派势必大规模走上街头。伊朗未来政治发展可能面临“街头民主”的严峻考验。 (本文来源：新京报 作者：张乐 徐超)<br />
