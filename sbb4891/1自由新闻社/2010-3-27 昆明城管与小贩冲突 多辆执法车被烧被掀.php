																											<div id="postmessage_535036" class="t_msgfont">昆明城管与小贩冲突 多辆执法车被烧被掀(组图)<br />
<a href="http://news.sohu.com/20100327/n271131858.shtml" target="_blank">http://news.sohu.com/20100327/n271131858.shtml</a><br />
 来源：新民晚报・新民网 2010年03月27日 <br />
被掀翻的车辆。 <br />
<br />
 <br />
 <br />
防爆队员赶到事发现场。 <br />
<br />
 <br />
 <br />
一名女城管队员被护送出来。 <br />
<br />
 <br />
 <br />
现场有多名人员被警方带走。 <br />
<br />
<br />
　　昨晚10时许，记者接到热线电话称，昆明北仓村大量群众和城管队员发生冲突。随后，记者赶到位于北市区银河大道旁北仓村事发现场时，多辆警车及城管的车停放在周围。数百人围着一辆微型货车大叫“打人的城管人员就在车里”“凶手出来……”随后，警察拉起了警戒线。不时有石块、砖块从人群中飞出，砸在微型货车上。现场警察试图制止但没有效果。记者从一名官方人士处得知，昨晚7时许城管在执法时导致一名老人受伤，但在民众的传言中，变成了“城管打死人”，于是导致了大范围冲突事件发生。<br />
<br />
　　现场多辆执法车被掀翻<br />
<br />
　　11时许，围观人员越来越多，有十多名身穿制服手持盾牌的人，进入警戒线后围住微型车，车门打开后，记者看到，有3名城管队员从车上下来，其中一名为女性。<br />
<br />
　　这时，人群中有人喊：“不能让他们走！”随后，大量石块便飞向城管队员。记者看到，一名城管队员头部流血，在同事搀扶下，准备上车离开。突然，一群人冲过来试图拦截城管队员离开，接着，几名身着制服的人转身与一中年男子发生冲突，现场顿时一片混乱。<br />
<br />
　　有人大叫“城管打人了！”随后有人拿着约1.5米长的棍子开始砸车玻璃。穿着制服的人开始从银河大道由北向南方向“撤退”。记者看到，约有100余人一边追赶一边扔石块，其中还有几名是年轻女子。一辆警车被人掀翻在银河大道上。<br />
<br />
　　一名手持盾牌的人，跑得慢了点，被十多人打翻在地。“不要打了，再打他就要死了。”其中一人说。随后见人群分开，有两人将被打者搀扶着离开现场。<br />
<br />
　　记者再次回到北仓村时，有4辆写有“综合执法”及“五华城管”等字样的车被掀翻，其中一辆在燃烧。随后赶到的消防官兵将火扑灭。<br />
<br />
　　随后，警方对该路段进行了交通管制，并有数十名警察及城管队员聚集在路上。一辆警车不停地在广播中向围观者喊话，让他们离开现场。<br />
<br />
　　今日凌晨1时40分许，记者在现场看到，数十人戴着手铐，被警察从巷子里押出来，上了警车。而20来名身穿制服的执法队员也撤离了现场。<br />
<br />
　　1时50分许，拖车将其中一辆被掀翻的车拉离现场。<br />
<br />
　　2时30分许，又有多名身穿制服的执法队员赶到现场。同城某媒体一名记者在混乱中被打伤。现场警方及城管负责人拒绝回答记者问题。<br />
<br />
　　截至记者发稿时，事态已得到初步控制，大批警察撤离现场。昆明警方表示，将积极处理此事。目前，正在做进一步调查。<br />
<br />
　　医院受伤老人神秘难寻<br />
<br />
　　昨晚11时40分许，记者通过其他渠道得知，受伤的老人大约50岁左右，后被送到解放军533医院外一科接受治疗。随后，记者立即赶到533医院外一科，值班医生称，他们没有收治在事故中受伤的老人。<br />
<br />
　　在医院外三科，一女医生称，听说有个受伤的老人，但不知道在哪个科室。在该院就诊室里，一名值班男医生向记者透露，该伤者好像是四川人，在两个小时前，有一批警察来医院，声称来找一名受伤的老人，“后来的情况就不清楚了”。另一位值班医生则称，该医院急诊室确实接到一名受伤的患者，但不知道转到哪个科室去了。</div>

							
							
							
							
