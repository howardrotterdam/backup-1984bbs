昆明信息港讯 今日，昆明市五华区政府及时通报了五华区关于城管部门整治占道经营摊点引发突发事件的相关情况。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;3月26日夜间至27日凌晨，昆明市五华区红云路北仓村农贸市场门口，城管部门在正常执法中与违法占道经营者发生纠纷，引发不明真相群众围观，并发生冲突。事件发生后，五华区委、区政府高度重视，区领导第一时间赶赴现场。目前，事件已得到有效处置，各项工作正在有序开展中，未发生新的情况。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;事件的具体情况是这样的：3月26日20时15分，昆明市综合行政执法局五华分局红云执法中队对辖区进行例行巡查，在巡查至占道经营摊点经常聚集、已进行过数次宣传教育的红云路北仓市场门口时，又发现在非机动车道上有四起占道经营摊点，执法人员依法进行处理，其中三名违法占道经营者接受了现场处罚，另有一骑三轮车的占道经营摊主杨胜秀（女，56岁，昭通籍）拒不接受处罚；执法人员拟依法暂扣三轮车，杨某骑车逃避，在拉扯过程中当事人倒地；此时，一些现场围观人员起哄“城管打死人了”，导致围观人群迅速聚集，情绪激动。为防止事态扩大，城管队员立即向公安部门报警（时间为20时35分左右），并联系了120 急救中心。警方赶到现场维持秩序，向当事人了解情况，耐心疏导现场围观人员；120急救车接通知后到现场把杨胜秀送往医院。<br />
&nbsp; &nbsp; <br />
