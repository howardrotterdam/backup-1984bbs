													<h2>昆明城管打下跪老太 民众激愤烧数量警车</h2>
						
						
																											<div id="postmessage_536201" class="t_msgfont">（图片已重置，详情请翻<a href="http://www.aboluowang.com/news/data/2010/0327/article_97059.html" target="_blank">http://www.aboluowang.com/news/data/2010/0327/article_97059.html</a>）<br />
<br />
民版：昆明城管打下跪老太 民众激愤烧数量警车<br />
<br />
大纪元记者骆亚采访报导）3月26日晚昆明市北市区北仓村农贸市场，昆明城管故意刁难下跪老太太并掀翻人力车致使重伤，激起民愤从而酿大规模群体流血事件。场面一度失控，数辆执法车被掀翻，二辆遭到焚烧，民众和警方的冲突一直延续到凌晨1、2点。 <br />
<br />
