																											<div id="postmessage_537381" class="t_msgfont">城管和民众中间的第三方不应缺席<br />
2010-03-29 08:02&nbsp; &nbsp;南方都市报&nbsp; &nbsp;网友评论 2 条，点击查看&nbsp; &nbsp; 我有话说<br />
<br />
　　[社论]<br />
<br />
　　3月26日夜间至27日凌晨，昆明市五华区城管部门在执法中与违法占道经营者发生纠纷，引发群众围观并发生冲突，导致10多人受伤，多辆汽车损坏。公安机关依法强行处置，将现场涉嫌“打、砸、烧”的40人带离现场进行盘问。<br />
<br />
　　新闻好像是旧闻，关于城管与摊贩乃至民众发生纠纷引爆冲突的事件，我们已经感觉疲劳。这种感觉所传递的，从根本上说是一种无奈：暴力为正常社会所不容，但我们却仿佛找不到阻遏的办法。<br />
<br />
　　如何化解城管执法者与相对人之间的这种紧张，专家学者早已贡献了很多意见。反思此次昆明事件，我们可以尝试换一种思路：如果在城管执法者和相对人之间，有一个平衡而中立的第三方，是否有利于大大缓解二者的紧张？如果在冲突之后，同样由平衡而中立的第三方出来主持调查、公布真相，再经由法律渠道依法处理，会不会减少长期暴力对抗而蓄积的戾气？<br />
<br />
　　这个思路一点儿也不新鲜，但其变成现实似乎难度不小。由当地政府发出的新闻通报中，事件的过程是这样的：摊贩占道经营，城管正常执法，50多岁的摊主杨胜秀“在拉扯中倒地”，不明真相的群众围观，引发冲突，执法者保持克制。<strong>而据媒体采访多位目击证人，却是细节迥然有异的另一幅图景：城管要对占道经营的杨胜秀罚款，杨交不出钱欲离开，一名体形偏胖的城管上前就将她的三轮车推了个四轮朝天。毫无防备的她倒在地上，翻过来的三轮车压住了她，从三轮车中滚出来的液化气罐砸在她的头部，致其昏迷。围观群众要求城管把老人送到医院，可城管上车就要走人，群众围住了城管的车子。城管一直不愿意送老人去医院，后来才发生了砸车和把车子掀翻的事情。</strong><br />
<br />
　　处理任何一起公众关注的事件，弄清真相是第一要务。但从现在的情况看，昆明的这起事件，很可能在众说纷纭中继续迷离。即使是在昆明，这也是有先例的。搜索一下就会发现，以往昆明城管与民众的这种冲突，不论闹到什么地步，最后居然都罕有一个公正、独立、具有公信力的调查结果。于是，哪怕是出了人命，一方面仍然是城管一方坚称的“正常执法”、“文明执法”，另一方面则是民间积累的对城管的愤怒。可以想象，这时候如果有一个平衡、中立的第三方，而且具有足够的权威，由它来进行独立的调查、仲裁和处理，又何至于两方非迎头相撞不可呢？<br />
<br />
　　我们认为，这个第三方，在冲突初起时，第一时间站出来的就应该是公安机关。面对城管执法者，抗拒执法应受制裁，这种制裁如果到了需使用暴力不可的地步，也只有公安机关才具备这种权力；但如果城管执法者公然对相对人使用暴力，这显然已不再是履行职务而导致的职务行为，其身份业已蜕变，转而成为了公安机关依据《治安处罚法》应该约束的对象。但这只是理论分析，现实生活中，遭遇暴力抗法的城管很容易受到警方保护，而摊贩在被城管追打中又有几人想起了报警？公安机关在这里为什么很少运用其依法处置的权力，值得深思。<br />
<br />
　　在冲突爆发之后，第三方的角色理应由政府承担，然而正如我们看到的，许多类似事件的善后处理，让政府客观、权威的形象受到了影响。这本来是不应该的，作为政府，城管是自己的一个部门，城管执法的相对人是自己权力的授予者，摆正自己的仲裁位置，并不是一件难事。<br />
<br />
　　政府要当好第三方，必须借助媒体，只有媒体全面而客观的报道，才能让真相昭然若揭。遗憾的是，本次事件中，前去采访的一位记者受到了棍击，虽然真相还不明朗，但可以认定，它对媒体帮助政府这个第三方独立调查真相是不利的。<br />
<br />
