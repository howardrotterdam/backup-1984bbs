记者27日上午来到发生冲突的红云路北仓村农贸市场门口，看到秩序已经恢复，附近居民如往常一样买菜、卖菜。<br />
<br />
租住在北仓村的李先生介绍，当时一位老人在北仓农贸市场外面卖炸洋芋，城管开了一辆车过来检查。“看到卖炸洋芋的老人占道经营后，城管要罚款，可老人没钱就推着车要走，有个长得有点胖的城管就掀翻老人的三轮车，卖炸洋芋的老人也倒在了地上。”看到老人倒在地上，有群众拨打了110的电话报警，还拨打了120的急救电话。<br />
<br />
李先生说：“老人倒在地上后昏迷，围观的群众要城管把老人送到医院看病，可城管上车就要走人，围观的群众看不过去，围住了城管的车子。城管一直不愿意送老人去医院，后来才发生了砸车和把车子掀翻在地的事情。”当时周边有些工人下班要经过北仓村旁边的红云路回家，事情发生后警方拉起了警戒线，一直到昨天凌晨2点50分以后警戒线才撤掉，还有人及时清理了路面。<br />
<br />
当事人<br />
<br />
三轮车被掀翻压身上动不得<br />
<br />
昨天凌晨2点，记者在解放军533医院找到了当事女摊贩，她名叫杨胜秀。她回忆了事发经过。当时她在街上用液化气灶炸洋芋卖，“先是来了一个瘦城管，没有动手打我，后来来了一个胖城管，一拳打在我头上。”杨胜秀说，城管把三轮车掀翻，自己被液化气罐砸到头部，倒了的车压在她身上，让她动弹不得。<br />
<br />
解放军533医院外三科值班的李医生介绍，杨胜秀老人经入院检查，浑身多处软组织挫伤，血压偏高、心脏区有疼痛感，他们已对她进行了吸氧、输液等治疗。经过一夜抢救治疗后，她的生命体征平稳，已经没有什么大碍了。早上，警察将其带离了医院，据说是去接受调查。<br />
<br />
相关<br />
<br />
记者采访被“强行处置”<br />
<br />
冲突中，生活新报采访中心主任熊熊亮明记者身份并退出警戒线后也被打伤。昨天下午3点，在云南省第三人民医院的病床上，熊熊的衣服上还可以看到前晚被打后留下的血迹，身上有淤青，双手受伤后已经包扎完毕，因为头部被打，10多个小时后熊熊依然感觉头昏。<br />
<br />
据当时就在现场的生活新报记者介绍，昨天凌晨2点多，熊熊站在警戒线外，看到路中间有一个女孩在哭，准备过去采访。这时，一伙手持警棍、拿盾牌、带着臂章的人冲上前来让众人往后退。熊熊说“退就退嘛”，话才说完就有一个人挥着警棍向他打来。他背过身护住头部表明自己是记者，但几个人并不理，把他推倒在地后向他的头部、身上打去。熊熊的手上多处肿胀，肩部、背部淤青，全身多处软组织受伤，需要留院观察。<br />
<br />
五华区政府召开的新闻通报会上，五华区政府副区长李英杰表示，在当晚现场处置过程中，生活新报的一名记者“不听劝阻，强行穿越警戒线，现场执法人员依法进行强行处置”。针对其当晚被“强行处置”，熊熊表示自己并没有进入警戒线内，他希望警方能公开自己被打的录像，尽快查明真相，“这样就一目了然，对大家都公平”。<br />
<br />
在云南省第三人民医院，还有另外一名被打伤的伤者李春梅，右眼眼角有淤青。李春梅就是熊熊当时准备采访的女孩。她今年17岁，在北市区右营的一家餐馆工作，前晚9点下班后，她和几个同事一起出去玩，后来走散了，“经过红云路时，我看到那边有很多人，正想找人问发生了什么事情，就有人冲过来说我骂了人，挥着棍子往我身上打。”<br />
