													<h2>政协委员严琦建议取缔社会网吧</h2>
						
						
																											<div id="postmessage_498441" class="t_msgfont">　　“各种网吧，管了多年，为什么管不好？”<br />
<br />
　　全国两会召开在即，全国政协委员、陶然居饮食集团董事长严琦语出惊人：关闭所有社会网吧，政府办公共网吧。<br />
<br />
　　就这个建议，严琦将在今年全国两会期间提交相关提案。“网吧衍生的各种社会问题，已经成了社会顽疾，针对顽疾，就应该下猛药。”<br />
<br />
　　一封来信<br />
<br />
<img src="http://img1.gtimg.com/tech/pics/29388/29388724.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　母亲：孩子逃课去打游戏<br />
<br />
　　全国两会召开前夕，严琦收到了一封母亲的来信。这位母亲在信中讲道：孩子上小学成绩一直很好，但上了初中，迷上了网络游戏。<br />
<br />
　　“孩子回家打游戏，逃课去打游戏。为了上网打游戏，孩子甚至宁愿饿肚子，也要上网买各种打游戏的装备。”<br />
<br />
　　这位母亲的来信，深深触动了严琦的心：“我也是母亲，这位母亲的心情，我能理解。”<br />
<br />
　　“针对各种社会网吧，全国各地包括重庆都出台了各种措施，比如为了防止未成年人上网，推行上网卡；为了防止网吧通宵营业，实行限时断网等措施。”<br />
<br />
　　严琦说，但现在看来，推行效果并不理想。<br />
<br />
　　一次调研<br />
<br />
　　严琦：不少小学生在网吧玩<br />
<br />
　　在接到这位母亲的来信后，严琦曾经实地到部分社会网吧作了一个调研。<br />
<br />
　　严琦说，在主城区一所小学的旁边，有两个网吧，严琦进去发现，有不少小学生在此上网，持卡上网的规定，在此成了一纸空文。<br />
<br />
　　进入网吧内，严琦仔细观察了一下，上网的学生，绝大部分是在打网络游戏；有部分人在上网聊天；还有部分人在浏览各种不良网站。<br />
<br />
　　严琦说，根据《2007年中国青少年网瘾数据报告》，青少年网民中有9.7%被认定染有网瘾，其中师生关系、家庭关系、同学关系不融洽的，均超过了20%；对在押未成年犯及劳教未成年人员的调查显示，54.1%的未成年人违法犯罪与网络相关，其中，35%的涉网违法犯罪基于缺少上网费用产生了作案动机，28.3%的涉网违法犯罪由于网络淫秽色情信息、暴力游戏的影响引发产生了作案冲动。<br />
<br />
　　严琦说，这说明很多社会问题的产生，都和网吧有关。为了牟利网吧忽视了本该承担的社会责任。<br />
<br />
　　一剂“猛药”<br />
<br />
　　建议：取缔各种社会网吧<br />
<br />
　　如何治疗网吧顽疾？<br />
<br />
　　严琦在今年的全国两会上将提交的提案，开出一剂“猛药”：建议取缔各种社会网吧。<br />
<br />
　　严琦说，各种社会网吧的开办，能带来部分税收，解决部分人员的就业问题，但这和其造成的社会负面影响相比，这点“好处”就显得微不足道了。<br />
<br />
　　因此，严琦说，政府必须采取强硬措施，强制关闭社会网吧。<br />
<br />
　　关闭了社会网吧，但在社会上，还有部分家庭和人群买不起电脑，他们由于工作和生活的正常需要，还有上网的需求，严琦建议，政府出资办一批公共网吧。<br />
<br />
　　“公办网吧，不以营利为目的，因此便于监管。”严琦说，公共网吧绝对禁止未成年人进入；公共网吧内安装不良信息过滤器，杜绝浏览黄色网站、暴力网站；在规定的时限内营业。<br />
<br />
　　关闭社会网吧<br />
<br />
　　开办网吧损失政府买单<br />
<br />
　　记者：社会网吧，解决了很大部分人群的就业问题，关闭社会网吧，这部分人群的就业怎么解决？<br />
<br />
　　严琦：在关闭社会网吧的同时，政府必须出台系列扶持措施，其中就包括就业措施。比如对已经在网吧的就业人员，可以由政府出资对其进行技能培训，提高他们的就业能力；创造更多的公共岗位，为他们提供就业的机会。<br />
<br />
　　记者：他们开办网吧的损失，谁来承担？<br />
<br />
　　严琦：由政府买单。为了社会的稳定，为了青少年的健康成长，这个单政府应该买，也有能力买。<br />
<br />
　　在关闭社会网吧的同时，政府要开办一批公共网吧，这些公共网吧就可以利用部分社会网吧的场地和设备，这样既弥补了网吧经营者的损失，同时也为开办公共网吧节约了时间和部分资金。<br />
<br />
　　网友：不能搞“一刀切”<br />
<br />
　　对此建议，昨日，部分老师、家长接受采访时表示赞成：政府办公共网吧，管理人员肯定会当好“守门神”，禁止未成年人进入网吧上网。<br />
<br />
　　昨日记者就此建议，在网上听了听部分网友的意见，大部分网友对此建议持赞成态度。不过，有网友表示，取缔社会网吧，需要一个过程，不能搞“一刀切”。<br />
<br />
　　网友“我的爱”说，如果一刀切，同时关闭了社会网吧，而政府公共网吧还不够多，这必然不能满足部分上网者的需求。因此，针对各个地区的不同，应该安排一个时间表，有一个缓冲期。<br />
<br />
　　网友细数网吧“四宗罪”<br />
<br />
　　昨日，一位网友在博客中细数了网吧的“四宗罪”。一是接纳未成年人现象屡见不鲜。二是网吧在校园周边200米范围内经营。三是有证网吧超时经营。四是存在无证经营网吧即“黑网吧”。</div>

							
							
							
							
