													<h2>中国游客辱骂德国馆是纳粹，世博德国馆声言将闭馆</h2>
						
						
																											<div id="postmessage_634357" class="t_msgfont">上海世博管理混乱令一些参展国家忍无可忍！继上海世博会捷克馆因不满会方管理混乱声言将闭馆之后，德国馆也发函警告世博局，因中国游客出言侮辱德国人是“纳粹”，甚至推撞工作人员，世博局如不改善管理秩序，德国馆将“无限期关闭”。<br />
<br />
据德国《南德意志报》引述德国馆首席专员舒密兹指出，从上海世博5月1日开园以来，德国馆就出现多宗不愉快的事件。他说，以开园第二天为例，因游客要进入德国馆排队需数小时，部份脾气暴躁的游客排得不耐烦便开始鼓噪，从花坛里拔出鲜花扔向德国馆，并试图从贵宾通道冲入展馆，被工作人员阻止后，他们便以普通话大喊德国人“纳粹”，其后推撞工作人员，甚至发生打斗。<br />
<br />
舒密兹致函上海世博局投诉上述情况“令人不能忍受”。“我亲眼看到我们的工作人员在展区内被人出言侮辱和人身攻击。”他在信中要求世博加强展馆保安，保障人员安全，否则德国馆将“无限期关闭”。<br />
<br />
德国馆的发言人说，很多人一对德国不高兴，就骂德国是纳粹。她说，当日发生冲撞事件后，馆方立即要求世博紧急服务中心支援，武警到场后平息打斗事件及控制人群。<br />
<br />
这位发言人还说，类似的混乱情况在园区内普遍存在，德国并不是唯一愤怒的一个展馆。据悉，同样位于C区的英国馆和瑞士馆也曾出现推撞情况，英国馆因此曾一度短暂关闭。<br />
<br />
捷克曾声言闭馆 <br />
<br />
另据港媒报导，较早前，捷克馆首席专员史特利克致函上海世博督导委员会，抗议园区管理的5项缺失，并多次使用“不可接受”及“荒谬”等强硬字眼。又声称问题如不能解决，捷克馆极可能在本月关门大吉。据了解，捷克馆事后获得上海世博局提出保证改善管理，方才打消闭馆念头。<br />
<br />
斯特赫利克的电邮在网上热传，他在信中直言不讳的称世博园区的管理不善。他表示，入口处每天大排长龙，也没有员工快速通道，连展馆工作人员每天花费数小时排队入园，“这是不可接受的。”<br />
<br />
还有让他无法接受的是，捷克馆工作人员办理在华工作签证时，在捷克馆提供所有所需资料后，世博有关部门拒绝发出确认信，以致展馆30名长期员工中，仅2人拿到在华居留证。其余人员将因商务签证到期而很快要离开中国。<br />
<br />
冯正虎欲观世博被带走 <br />
<br />
上海维权人士冯正虎原计划周五到世博园参观，但出发时被国保带走。他的家人表示，至今仍未能跟他取得联系，虽然忧心，但仍然支持他的决定。冯正虎出发到世博园前向传媒表示，不会在世博园内抗议，强调有权利到场参观，不用向政府申请。<br />
<br />
冯正虎的哥哥冯正龙向自由亚洲电台表示，得知弟弟遭国保带走后，他多次致电弟弟仍未能取得联络，感到非常忧心。他说冯正虎自从在日本回国以来，一直被限制人身自由。<br />
<br />
据了解，已购票的上海维权人士冯正虎，计划周五一人前往世博园参观，但周四已遭居所外监视的国保警告不要前往。但冯正虎仍坚称会按计划入世博游玩，强调不会带传单横幅在世博园内抗议，期望上海当局尊重他的意愿。<br />
<br />
冯正虎于上月18日在网上发表“冯正虎冤假错案博览会的规划启事”，表示在世博会期间，会例举他个人经历的12件典型案例作展览，以展示上海的司法不公。此后，冯正虎多次遭到警方传讯，警方更拿走了冯正虎4台电脑、打印机等物件。警方更发出警告，可以让他像高智晟一样消失。</div>

							
							
							
							
