&nbsp; &nbsp; “我听到唐福珍在喊：‘不要上来，不要逼我，否则我死给你们看。’看到唐福珍在往身上浇液体的东西，估计是汽油，下边一个声音在喊：‘你这是违法犯罪，现在下来还来得及。’不一会儿，唐福珍全身燃烧起来，十几秒后，消防队员用喷水枪喷向阁楼，之后不久，唐福珍被抬下来，烧得没了人样，唐福珍的大哥唐福成也满脸是血躺在地上，两个人一块儿被等候在旁边的救护车拉走。”<br />
 <br />
&nbsp; &nbsp; 唐福珍的邻居邓尤德说，我听到唐福珍一遍遍喊话，看到她两次朝自己身上浇汽油，想以死来阻止那些人进攻，但那些人完全不理会，他们锯开三楼防盗门，头戴钢盔，手持棍棒，冲上三楼楼顶，唐家的人都躲在楼顶平台上，两边人交汇在一起，顿时乱成一锅粥，吼叫声、打斗声、妇女孩子的哭叫声混成一片，站在阁楼上的唐福珍对三楼平台发生的一切看得清清楚楚，她情绪很激动，再次把油桶举过头顶，将剩下的汽油全部浇在身上，她是万般无奈才“自焚”的。唐福珍被抬走后，破拆始终没有停止，她家后院平房的物品被多辆货车拉走，而楼房里的很多家居用品甚至贵重物品被破碎机直接埋在废墟中。<br />
 <br />
&nbsp; &nbsp; 魏交是唐福珍的外甥媳妇，一位有着不满周岁婴儿的年轻母亲，她当时恰在唐福珍家暂住，亲身感受了那个噩梦般的早晨。在向新湘报记者讲述亲身经历时，魏交全身发抖，数度哽咽抽泣。<br />
 <br />
&nbsp; &nbsp; 她说：“当时场面很乱，我们一家人老少十几口都退到三楼平台上，我抱着娃娃不停游走，生怕伤害到宝宝。突然，三楼通向平台的铁门被破开，一群头戴钢盔、手持盾牌棍棒的人冲了上来，他们见人就打，我躲避不及，被他们一棍敲在左小腿上，我直接倒在地上，宝宝吓得“哇哇”大哭，还没等我站起来，就觉得眼睛被人喷了什么东西，顿时感觉眼睛刺疼，呼吸困难，他们从怀中夺走了我的孩子，又朝我腰部和腿部踢了几脚，模糊中，我看到地上躺着都是我们的人，包括我六十多岁的婆婆。”<br />
 <br />
&nbsp; &nbsp; “之后，他们把我抬到楼下的救护车上，过了一会儿，大舅唐福成和三姨唐福珍也被抬到救护车上。”<br />
 <br />
&nbsp; &nbsp; “我看到三姨唐福珍的头发睫毛全烧掉了，脸上皮肤烧得爆开，露出布满血丝的红肉，牙齿漆黑，眼睛凸起，身上的衣服烧烂了，全身血淋淋地，见不到一块完整的皮肤。”<br />
 <br />
&nbsp; &nbsp; “三姨唐福珍在我婆婆姊妹三人中长得最漂亮，平时也很会打扮、很爱美的一个人，一下变成这样，谁能接受得了？我哭着不停地喊她，终于，她有了反应，她呻吟着说：‘娇娇，我要死了，胡柯（儿子）在哪？你大姨是不是被他们打死了？我死后，用我身份证上那张照片做遗像。’随后，不放心地用足力气又叮嘱道：‘一定要从我家里选一张最漂亮的照片做遗像！’说完就昏了过去。”<br />
 <br />
&nbsp; &nbsp; 魏交擦干泪水，挽起裤管，记者清楚地看到她左小腿弯处有一大块条状的紫痕。<br />
 <br />
&nbsp; &nbsp; 唐福成是唐福珍的大哥，今年60多岁，他身材瘦弱，躺在成都军区总医院的病床上，除了头面部伤以外，他还向记者展示了全身多处软组织损伤。唐福成说：我看到他们把我二妹头打破了，就去夺棍子，没想到，被人用棒子打在太阳穴部，一下子就昏过去了。<br />
 <br />
&nbsp; &nbsp; 据唐福珍的丈夫胡昌明向《新湘报》反映，这次事件后，胡家有四位亲人受伤住院，其中两人伤情严重，住在重症监护室，七人被公安人员带走，未给法律文书，至今下落不明，他们分别是儿子胡柯、七哥胡昌贵、侄子胡正勇、侄女胡正英、侄女婿吴炳康、儿姨姐唐会青和内侄刘军。这七人当时都被打了，有的伤势还相当严重，经过多方打听，只得到他们没有羁押在看守所、而是被囚禁在某家旅馆的消息。<br />
 <br />
<br />
 <br />
数十警察站在楼下观望女主人自焚<br />
 <br />
&nbsp; &nbsp; 一条弓形路&nbsp; &nbsp;使胡昌明变成“钉子户”<br />
 <br />
&nbsp; &nbsp; 胡昌明，天回镇金华村人，成都奥仕威实业有限公司法人代表，他对记者详细讲述了企业的变迁及成为“钉子户”的历史渊源。<br />
 <br />
&nbsp; &nbsp; 1996年，金华村还是典型的农村面貌，闭塞、落后，胡昌明和妻子唐福珍当时在成都市区做服装生意，已具有相当规模。一天，村党支部书记陈茂源和村主任张国富找到他，说天回乡和金华村公布了建设规划方案，面向在外经营的本地成功人士和各界人士招商引资，兴建一条“农民街”，以促进地方经济发展，使用土地政策优惠，村里可统一办理房地产手续。胡昌明说他当时生意做的很好，此时搬迁势必受到影响，但出于对家乡的热爱和对“父母官”的信任，他毅然撤回资金，回家乡投资办厂。<br />
 <br />
&nbsp; &nbsp; 首期胡昌明以5万元的优惠价格和村委会签订了969.8平方米的《建房用地合同》，考虑到建房是百年大计，他采取了最牢固的建筑方案，先后投资数百万元，做了精心的设计和装修，建起一幢2000多平方米的综合楼，一、二层做车间，三层办公家人居住，一家服装加工厂迅速开办。1998年，胡昌明正式注册了成都奥仕威实业有限公司，员工多达100多人，不但为当地农民提供了就业机会，还为当地政府带来了税源。1999年，为扩大生产，他又与村委会签订了租赁房后1.8亩土地50年的合同，用于车间和工人宿舍建设。<br />
 <br />
&nbsp; &nbsp; 在那几年，胡昌明夫妇成为政府招商引资成功的一面旗帜，报纸上有名，电视上有影，胡昌明被当地政府授予“勤劳致富带头人”等荣誉称号，妻子唐福珍还被区妇联评为女性自主创业模范。<br />
 <br />
&nbsp; &nbsp; 胡昌明对新湘报说，在此期间，他为办理房地产手续一直奔波于政府有关部门，交足了************费用，提供了相关材料，但由于职能部门的推诿扯皮，房地产证书最终没能获取。2005年，金牛城乡一体化后，政府决定修路，停止办理房屋产权手续，胡昌明企业的土地使用证和房屋产权证成为历史遗留问题。<br />
 <br />
&nbsp; &nbsp; 2005年7月，街道办相关领导找到胡昌明，说胡的企业用房是违章建筑，因为修路，需要拆除，当时只答应补偿90万元，胡昌明坚决不同意，2007年，经重新调查测算，调整为155万元，2008年3月，拆迁部门根据房屋结构和装修较好等因素，补偿费提高至217万元，仍未能达成协议。<br />
 <br />
&nbsp; &nbsp; 胡昌明认为，我企业是招商引资来的合法企业，企业占用的是农村集体土地，而纳入城市规划在九年后，土地房产等证书没能办下责任不在企业，怎能不考虑历史因素，武断地定性为违章建筑呢？为了企业的基础建设，我花费了大量的精力、财力，陆续投入700余万元，区区217万元，不足总投资的三分之一，实在难以接受。<br />
 <br />
&nbsp; &nbsp; 更令胡昌明心理难以平衡的是，修“金新路”本应在原路基础上扩展，而规划者却偏偏对老路弃之不用，绕了一个弯，修成弓字形，把胡昌明的企业冲掉。胡昌明看到对面同样没有任何产权证书、和乡村干部有关的楼房保留下来，心中愤愤不平。<br />
 <br />
&nbsp; &nbsp; 胡昌明是退伍军人，性情耿直，认为修弯弯路，是保护干部利益而伤害百姓利益，更浪费了国家资源。从2006年起，他就开始走向上访告状路，企业无心打理，逐渐出现负债经营而最终停业。同时，胡昌明因此也得罪了地方“父母官”，地方领导视其为“眼中钉、肉中刺”，处心积虑，欲除之而后快。<br />
2007年10月23日，成都市金牛区城市管理行政执法局给胡昌明下达了限期拆除违法建设事先通知书，10月29日，又给他下达了限期拆除违法建设决定书，限令胡昌明在2007年11月3日24时前拆除。胡昌明未予理睬。<br />
 <br />
&nbsp; &nbsp; 2009年4月10日上午，金牛区刘区长、廖主任以谈拆迁补偿为名，把胡昌明骗到街道办，由派出所长带人控制起来。同时，城管执法局局长钟昌林带领大队人马光天化日闯入胡家，他们撬开卷闸门，剪断大门铁锁，割开防盗门，搬家公司的车和破拆机械也开进来，试图强行拆除，唐福珍难以抵挡，便全身淋遍汽油登上楼顶，声称自焚，才吓退了那些人。<br />
 <br />
&nbsp; &nbsp; 从此，胡昌明夫妻成了令政府官员头疼的“钉子户”，胡昌明说：我不想做“钉子户”，更不会漫天要价，已做好让步的思想准备，但政府要合理公正、补偿适当，不要使我们损失太多。<br />
 <br />
&nbsp; &nbsp; 此前，在胡昌明的控诉材料中，一直把冲自己企业的这条路称为S路，经记者实地察看，确实发现在铁路桥和河流桥之间的400米路段新修了一条弯路，这条路与老路两头对接，中间弯起，胡昌明的企业恰在这个弯上，但从整体上看，用C形路或弓形路描述更为贴切。<br />
 <br />
&nbsp; &nbsp; 胡昌明向记者提供了多份加盖村委会和金牛区规划、土地等管理部门**的证据材料，以证明胡昌明所言不缪。<br />
 <br />
<br />
 <br />
阻拦记者离开的街道办女主任<br />
 <br />
&nbsp; &nbsp; 生命、人性、法律与政绩、领导意志<br />
 <br />
&nbsp; &nbsp; 记者在调查采访“11、13”事件过程中，深深感觉到：在成都市金牛区，长官意志和政绩是多么地重要，而百姓的生命被漠视，党中央、国务院倡导的人性化服务和国家法律被远远抛在一边。<br />
 <br />
&nbsp; &nbsp; 据目击的村民反映，当时在现场指挥政府官员有金牛区城管局长、区长助理钟昌林，天回乡政府办主任车成志，天回乡武装部长主管拆迁工作的李纯明，还有一个职别更高的，因为这些人都听他安排，但村民不认识。组成人员有公安、消防、施工人员及穿迷彩服的不明身份人员大约300多人，处理完唐家的人后，穿迷彩服的人最先撤离，他们很多人将迷彩服抛在路边地里，然后乘数辆德阳市牌照的客车离去，村民怀疑是政府雇佣的外地社会不良人员。<br />
 <br />
&nbsp; &nbsp; 在成都军区总医院，记者探望“11、13”事件伤员时，见到有数名着类似保安服装的人徘徊在侧，名为伺候实为看守，限制伤者自由，阻断与外界的联系，记者采访完其中一名伤员欲离开时，被他们强行扣留一个多小时：“你们要等我们领导来，否则不得离开半步！”其他伤者更是无法见到。<br />
 <br />
&nbsp; &nbsp; 事件发生时，该幢楼房的男主人胡昌明正在北京上访，侥幸得以平安。11月16日，记者在成都见到胡昌明，他说金牛区政府害得他家破人亡不说，还要赶尽杀绝，正在四处抓他，只好每天东躲西藏，他很想见到医院妻子唐福珍一面，却又怕自己被抓后连个伸冤告状的人也没有了。然而，就在第二天傍晚，胡昌明的亲戚打电话告诉记者：“躲到绵阳的胡昌明被金牛警方抓走了，什么罪名不得而知。”<br />
 <br />
&nbsp; &nbsp; 一个下过两次病危通知书的垂危病人，最需要亲人的关怀和照料，而先抓其子，后抓其夫，其心何忍？情何以堪？<br />
 <br />
&nbsp; &nbsp; 胡昌明企业的后期建筑不影响金新路改道，不在拆迁范围，而其妻自焚后，破拆人员一鼓作气，竟将后期建筑也荡为平地，胡昌明一家真的是片瓦无存、无家可归了。<br />
 <br />
&nbsp; &nbsp; 胡昌明一家为了************强制拆迁，在家中及楼顶等醒目位置插有国旗，然而，国旗未能阻止住破门而入的破拆队伍，大家也是眼睁睁地看着唐福珍在国旗旁自焚的，一位法学专家说的好：尽管我国的法律不断健全完善，但一些地方政府为了政绩不惜违法，在那个地方可以说是“有法无天”。<br />
 <br />
<br />
 <br />
女主人在屋顶上“操作”很久竟无人施救<br />
 <br />
&nbsp; &nbsp; 我国《物权法》第六十六条规定，私人的合法财产受法律保护，禁止任何单位和个人侵占、哄抢、破坏。<br />
 <br />
&nbsp; &nbsp; 北京市长歌律师事务所资深律师刘亚军认为：“胡昌明的房屋如果当地国土、规划等管理部门认定为违法建筑，应根据相关法规予以处罚，并允许胡昌明对相关处罚提起诉讼，如果处罚最终被依法认定为合法，管理部门可以申请人民法院执行相关处罚决定，应该说区政府本身不具有实施强制拆迁的主体资格的，同时，也没有任何合法的拆迁手续，成都市金牛区政府的行为，与我国现行的法律法规是严重的相违背的，组织和实施的相关行为人的行为已经涉嫌犯罪。”<br />
 <br />
&nbsp; &nbsp; “我国的《物权法》和《城市拆迁管理条》之间存在一定的立法矛盾，但是这种矛盾也并不是不能在法律实施过程中予以避免的。现代的法治理念是以人为本，在财产利益和人身权利发生冲突的时候，首先应该考虑人的生命和健康。本案的发生，究其原因，是地方政府的某些官员把政绩放在首位，根本不把群众的利益放在心上。巨大的经济利益的驱动，已经使很多地方的官员利令智昏。就本案而言，大批的地方官员在现场，眼看着一个被拆迁人举火自焚，竟然还下强制拆除的命令，就不仅仅是法律理念的缺失了。如果金牛区政府领导能真正从维护社会稳定、促进经济发展的角度去思考问题，像唐福珍这样的悲剧还是可以避免的。”（来源：新湘报）<br />
