													<h2>成都一女企业家因拆迁楼顶自焚</h2>
						
						
																											<div id="postmessage_342877" class="t_msgfont">11月13日早晨，在成都市金牛区天回镇街道办金华村发生一起恶性“拆迁”事件，女主人以死相争未能阻止政府组织的破拆队伍，最后“自焚”楼顶天台，烧得面目全非、命悬一线。数人被拘，数人受伤住院，政府部门定性为暴力抗法，被拆户控诉政府暴力“拆迁”，究竟孰是孰非并不重要，重要的是生命在这里被漠视，法律在这里被践踏，本应预见的流血事件却在政府官员的现场指挥下发生了。<br />
《新湘报》特约记者 刘立民<br />
女主人以命相争&nbsp; &nbsp;自焚楼顶天台<br />
“你们退下，我们可以坐下来商量，否则我就要自焚了！”初冬的晨曦中，一位中年妇女身穿睡袍，站在三层楼顶阁楼的天台上，身后的国旗在轻风中飘动，只见她一次次举起油桶，把汽油浇在身上，一边不停地用喇叭向下喊话。<br />
这是一座三层楼房，坚实的结构，漂亮的外部装修，显示出房主人倾注的心血。楼下，机器轰鸣，正在破拆；楼前，无数消防、公安官兵严阵以待，不远处停放着救护车，医护人员也做好了准备。三层楼顶平台上人头攒动，不时有戴钢盔的人出现，接着有浓烟火光冒起，还传来鞭炮的噼啪声。<br />
“唐姐，你要冷静！不要与政府作对，你现在下来还来得及。”楼下，也有人在向那位中年妇女喊话。<br />
 十几分钟后，另外有人爬上了阁楼，似乎要制止她的行为，瞬间，惨烈的一幕出现了，那位妇女变成一个通红的火人，在高高的楼顶上跳动、挣扎------<br />
这是11月13日成都市金牛区天回镇金华村拆房现场的一段录像，自焚的这位妇女叫唐福珍，今年47岁，属于重三度烧伤和吸入式烧伤，已行气管切开术，至今还住在成都军区总医院烧伤科重症监护室，尚未脱离生命危险。<br />
<img src="http://i47.tinypic.com/152kg91.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
数十警察站在楼下观望女主人自焚<br />
<br />
<br />
暴力执法还是暴力抗法？&nbsp;&nbsp;<br />
记者在成都市金牛区政府采访时，已经事过三天，据外宣办工作人员说，11月13日对唐福珍楼房的强制破拆是政府组织的“拆违”行动，由于受到对方人员的暴力阻止，有4、5名工作人员受伤和机械损坏，已经定性为“11、13”暴力抗法事件，有现场录像为证，记者要求观看录像和探访受伤人员，被婉言拒绝。在天回镇金华村金新路，记者看到那幢壮观的楼房不复存在，现场只留下残砖断瓦，还有挖掘机正在作业，目击者及当事人回忆起当时发生的情况依然心惊肉跳，欷[不止。<br />
金华村村民严某说：“11月13日早6点半左右，天蒙蒙亮，我去上班经过唐福珍的家门口，看到唐福珍站在顶层阁楼上，旁边的老年活动中心停了很多车辆，四周围了很多陌生人，有穿警服的，有穿迷彩服的。一会儿，一辆挖掘机开始挖唐福珍家的大门和围墙，大门挖开后，我看到两个穿************的人把胡昌发拖了出来，几个穿迷彩服的人搭竹梯强行从窗户进入房间，一些床上用品从窗口抛下来，还有一些手拿盾牌和棒棒，戴着钢盔的人进去，当时整栋楼里到处是玻璃等东西破碎的声音。”<br />
“我听到唐福珍在喊：‘不要上来，不要逼我，否则我死给你们看。’看到唐福珍在往身上浇液体的东西，估计是汽油，下边一个声音在喊：‘你这是违法犯罪，现在下来还来得及。’不一会儿，唐福珍全身燃烧起来，十几秒后，消防队员用喷水枪喷向阁楼，之后不久，唐福珍被抬下来，烧得没了人样，唐福珍的大哥唐福成也满脸是血躺在地上，两个人一块儿被等候在旁边的救护车拉走。”<br />
唐福珍的邻居邓尤德说，我听到唐福珍一遍遍喊话，看到她两次朝自己身上浇汽油，想以死来阻止那些人进攻，但那些人完全不理会，他们锯开三楼防盗门，头戴钢盔，手持棍棒，冲上三楼楼顶，唐家的人都躲在楼顶平台上，两边人交汇在一起，顿时乱成一锅粥，吼叫声、打斗声、妇女孩子的哭叫声混成一片，站在阁楼上的唐福珍对三楼平台发生的一切看得清清楚楚，她情绪很激动，再次把油桶举过头顶，将剩下的汽油全部浇在身上，她是万般无奈才“自焚”的。唐福珍被抬走后，破拆始终没有停止，她家后院平房的物品被多辆货车拉走，而楼房里的很多家居用品甚至贵重物品被破碎机直接埋在废墟中。<br />
魏交是唐福珍的外甥媳妇，一位有着不满周岁婴儿的年轻母亲，她当时恰在唐福珍家暂住，亲身感受了那个噩梦般的早晨。在向新湘报记者讲述亲身经历时，魏交全身发抖，数度哽咽抽泣。 她说：“当时场面很乱，我们一家人老少十几口都退到三楼平台上，我抱着娃娃不停游走，生怕伤害到宝宝。突然，三楼通向平台的铁门被破开，一群头戴钢盔、手持盾牌棍棒的人冲了上来，他们见人就打，我躲避不及，被他们一棍敲在左小腿上，我直接倒在地上，宝宝吓得“哇哇”大哭，还没等我站起来，就觉得眼睛被人喷了什么东西，顿时感觉眼睛刺疼，呼吸困难，他们从怀中夺走了我的孩子，又朝我腰部和腿部踢了几脚，模糊中，我看到地上躺着都是我们的人，包括我六十多岁的婆婆。”<br />
“之后，他们把我抬到楼下的救护车上，过了一会儿，大舅唐福成和三姨唐福珍也被抬到救护车上。”<br />
“我看到三姨唐福珍的头发睫毛全烧掉了，脸上皮肤烧得爆开，露出布满血丝的红肉，牙齿漆黑，眼睛凸起，身上的衣服烧烂了，全身血淋淋地，见不到一块完整的皮肤。”<br />
“三姨唐福珍在我婆婆姊妹三人中长得最漂亮，平时也很会打扮、很爱美的一个人，一下变成这样，谁能接受得了？我哭着不停地喊她，终于，她有了反应，她呻吟着说：‘娇娇，我要死了，胡柯（儿子）在哪？你大姨是不是被他们打死了？我死后，用我身份证上那张照片做遗像。’随后，不放心地用足力气又叮嘱道：‘一定要从我家里选一张最漂亮的照片做遗像！’说完就昏了过去。”<br />
 魏交擦干泪水，挽起裤管，记者清楚地看到她左小腿弯处有一大块条状的紫痕。<br />
唐福成是唐福珍的大哥，今年60多岁，他身材瘦弱，躺在成都军区总医院的病床上，除了头面部伤以外，他还向记者展示了全身多处软组织损伤。唐福成说：我看到他们把我二妹头打破了，就去夺棍子，没想到，被人用棒子打在太阳穴部，一下子就昏过去了。<br />
据唐福珍的丈夫胡昌明向《新湘报》反映，这次事件后，胡家有四位亲人受伤住院，其中两人伤情严重，住在重症监护室，七人被公安人员带走，未给法律文书，至今下落不明，他们分别是儿子胡柯、七哥胡昌贵、侄子胡正勇、侄女胡正英、侄女婿吴炳康、儿姨姐唐会青和内侄刘军。这七人当时都被打了，有的伤势还相当严重，经过多方打听，只得到他们没有羁押在看守所、而是被囚禁在某家旅馆的消息。<br />
<img src="http://i47.tinypic.com/23slf5.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
女士自焚的全过程<br />
一条弓形路&nbsp; &nbsp;使胡昌明变成“钉子户”<br />
胡昌明，天回镇金华村人，成都奥仕威实业有限公司法人代表，他对记者详细讲述了企业的变迁及成为“钉子户”的历史渊源。<br />
1996年，金华村还是典型的农村面貌，闭塞、落后，胡昌明和妻子唐福珍当时在成都市区做服装生意，已具有相当规模。一天，村党支部书记陈茂源和村主任张国富找到他，说天回乡和金华村公布了建设规划方案，面向在外经营的本地成功人士和各界人士招商引资，兴建一条“农民街”，以促进地方经济发展，使用土地政策优惠，村里可统一办理房地产手续。胡昌明说他当时生意做的很好，此时搬迁势必受到影响，但出于对家乡的热爱和对“父母官”的信任，他毅然撤回资金，回家乡投资办厂。<br />
首期胡昌明以5万元的优惠价格和村委会签订了969.8平方米的《建房用地合同》，考虑到建房是百年大计，他采取了最牢固的建筑方案，先后投资数百万元，做了精心的设计和装修，建起一幢2000多平方米的综合楼，一、二层做车间，三层办公家人居住，一家服装加工厂迅速开办。1998年，胡昌明正式注册了成都奥仕威实业有限公司，员工多达100多人，不但为当地农民提供了就业机会，还为当地政府带来了税源。1999年，为扩大生产，他又与村委会签订了租赁房后1.8亩土地50年的合同，用于车间和工人宿舍建设。<br />
在那几年，胡昌明夫妇成为政府招商引资成功的一面旗帜，报纸上有名，电视上有影，胡昌明被当地政府授予“勤劳致富带头人”等荣誉称号，妻子唐福珍还被区妇联评为女性自主创业模范。<br />
胡昌明对新湘报说，在此期间，他为办理房地产手续一直奔波于政府有关部门，交足了************费用，提供了相关材料，但由于职能部门的推诿扯皮，房地产证书最终没能获取。2005年，金牛城乡一体化后，政府决定修路，停止办理房屋产权手续，胡昌明企业的土地使用证和房屋产权证成为历史遗留问题。<br />
2005年7月，街道办相关领导找到胡昌明，说胡的企业用房是违章建筑，因为修路，需要拆除，当时只答应补偿90万元，胡昌明坚决不同意，2007年，经重新调查测算，调整为155万元，2008年3月，拆迁部门根据房屋结构和装修较好等因素，补偿费提高至217万元，仍未能达成协议。胡昌明认为，我企业是招商引资来的合法企业，企业占用的是农村集体土地，而纳入城市规划在九年后，土地房产等证书没能办下责任不在企业，怎能不考虑历史因素，武断地定性为违章建筑呢？为了企业的基础建设，我花费了大量的精力、财力，陆续投入700余万元，区区217万元，不足总投资的三分之一，实在难以接受。<br />
更令胡昌明心理难以平衡的是，修“金新路”本应在原路基础上扩展，而规划者却偏偏对老路弃之不用，绕了一个弯，修成弓字形，把胡昌明的企业冲掉。胡昌明看到对面同样没有任何产权证书、和乡村干部有关的楼房保留下来，心中愤愤不平。<br />
胡昌明是退伍军人，性情耿直，认为修弯弯路，是保护干部利益而伤害百姓利益，更浪费了国家资源。从2006年起，他就开始走向上访告状路，企业无心打理，逐渐出现负债经营而最终停业。同时，胡昌明因此也得罪了地方“父母官”，地方领导视其为“眼中钉、肉中刺”，处心积虑，欲除之而后快。<br />
2007年10月23日，成都市金牛区城市管理行政执法局给胡昌明下达了限期拆除违法建设事先通知书，10月29日，又给他下达了限期拆除违法建设决定书，限令胡昌明在2007年11月3日24时前拆除。胡昌明未予理睬。<br />
2009年4月10日上午，金牛区刘区长、廖主任以谈拆迁补偿为名，把胡昌明骗到街道办，由派出所长带人控制起来。同时，城管执法局局长钟昌林带领大队人马光天化日闯入胡家，他们撬开卷闸门，剪断大门铁锁，割开防盗门，搬家公司的车和破拆机械也开进来，试图强行拆除，唐福珍难以抵挡，便全身淋遍汽油登上楼顶，声称自焚，才吓退了那些人。<br />
 从此，胡昌明夫妻成了令政府官员头疼的“钉子户”，胡昌明说：我不想做“钉子户”，更不会漫天要价，已做好让步的思想准备，但政府要合理公正、补偿适当，不要使我们损失太多。<br />
此前，在胡昌明的控诉材料中，一直把冲自己企业的这条路称为S路，经记者实地察看，确实发现在铁路桥和河流桥之间的400米路段新修了一条弯路，这条路与老路两头对接，中间弯起，胡昌明的企业恰在这个弯上，但从整体上看，用C形路或弓形路描述更为贴切。<br />
胡昌明向记者提供了多份加盖村委会和金牛区规划、土地等管理部门**的证据材料，以证明胡昌明所言不缪。<br />
<img src="http://i50.tinypic.com/2urbbck.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
阻拦记者离开的街道办女主任<br />
<br />
生命、人性、法律与政绩、领导意志<br />
记者在调查采访“11、13”事件过程中，深深感觉到：在成都市金牛区，长官意志和政绩是多么地重要，而百姓的生命被漠视，党中央、国务院倡导的人性化服务和国家法律被远远抛在一边。<br />
据目击的村民反映，当时在现场指挥政府官员有金牛区城管局长、区长助理钟昌林，天回乡政府办主任车成志，天回乡武装部长主管拆迁工作的李纯明，还有一个职别更高的，因为这些人都听他安排，但村民不认识。组成人员有公安、消防、施工人员及穿迷彩服的不明身份人员大约300多人，处理完唐家的人后，穿迷彩服的人最先撤离，他们很多人将迷彩服抛在路边地里，然后乘数辆德阳市牌照的客车离去，村民怀疑是政府雇佣的外地社会不良人员。<br />
在成都军区总医院，记者探望“11、13”事件伤员时，见到有数名着类似保安服装的人徘徊在侧，名为伺候实为看守，限制伤者自由，阻断与外界的联系，记者采访完其中一名伤员欲离开时，被他们强行扣留一个多小时：“你们要等我们领导来，否则不得离开半步！”其他伤者更是无法见到。<br />
事件发生时，该幢楼房的男主人胡昌明正在北京上访，侥幸得以平安。11月16日，记者在成都见到胡昌明，他说金牛区政府害得他家破人亡不说，还要赶尽杀绝，正在四处抓他，只好每天东躲西藏，他很想见到医院妻子唐福珍一面，却又怕自己被抓后连个伸冤告状的人也没有了。然而，就在第二天傍晚，胡昌明的亲戚打电话告诉记者：“躲到绵阳的胡昌明被金牛警方抓走了，什么罪名不得而知。”一个下过两次病危通知书的垂危病人，最需要亲人的关怀和照料，而先抓其子，后抓其夫，其心何忍？情何以堪？<br />
胡昌明企业的后期建筑不影响金新路改道，不在拆迁范围，而其妻自焚后，破拆人员一鼓作气，竟将后期建筑也荡为平地，胡昌明一家真的是片瓦无存、无家可归了。<br />
