女主人打出的标语“房在人在与房屋共存亡”一语成谶 <br />
 <br />
&nbsp; &nbsp; 据唐福珍的妹妹唐福英向《新湘报》反映，自从11月13日发生自焚事件后，唐福珍病房（重症监护室）日夜有4名治保人员监守，她及其他亲人根本见不到姐姐。11月29日下午，她侧面听说三姐唐福珍病危，晚上七点钟左右，唐福英急急忙忙赶到成都军区总医院，看到楼下大批警察站着，四楼烧伤科门口也有无数统一着装的治保人员守着，唐福英要探望姐姐，被天回镇派出所曾所长挡在门外。而后胡昌明和儿子胡焱熹（胡柯）在ZF官员和警察的看押下来到病房，见唐福珍最后一面，唐福英才被允许一起进病房。当时看到医生正在唐福珍的胸部按压，她就悲愤过度，当时就昏过去了。<br />
&nbsp; &nbsp; 唐福珍的另外一位亲戚告诉新湘报记者，“晚上9点左右，我看见金牛区副区长刘岩、区ZF办公室张主任、天回街道办宋传福书记等领导干部来到医院现场，当时的陆军总医院已经戒严，有10多辆警车在楼下，警察和治保人员、城管人员共计百人左右严阵以待。我们强烈要求ZF把羁押在看守所的唐福珍的其他亲戚带来，见死者最后一面，但这个请求始终没有答应。”<br />
&nbsp; &nbsp; “非常蹊跷的是，在唐福珍死亡数小时后、直到晚上11点左右医院才宣布唐福珍死亡。”唐福珍的亲戚要求把尸体交给亲属，成都市金牛区刘岩副区长称要请示领导，反反复复到第二天凌晨1时左右，才同意亲戚把唐福珍的尸体运往新都区东林殡仪馆安放，“当时有包括警车在内的6、7辆车和警察、治保人员、城管人员共40人左右护送，尸体安放后ZF却留下10多名治保人员监守。”<br />
&nbsp; &nbsp; 从家属传来的现场视频中，记者看到唐福珍的亲属被一个领导干部模样的人和众治保人员挡在走廊里，悲恸的哭声令人心颤，还有不断的质问：“亲人死去了都不让见一面，你们还有人性吗？”以及那位干部的声音：“你们要冷静，我要请示一下领导-----”&nbsp;&nbsp;<br />
&nbsp; &nbsp; 唐福珍的三哥唐福明告诉《新湘报》记者：“三妹死的那么惨，而亲属要回尸体本是人之常情，却在金牛区ZF官员的阻止下是那么艰难。唐福珍的家已经荡平成为施工现场，她的灵堂设在哪都是问题。还有，唐福珍的家人只剩下12岁的女儿是自由的，而她在外地上学，家里发生这么大的事一直瞒着她，不知道她听说后能否经受住如此重大的打击，唐福珍又有谁为她守灵送葬呀？”<br />
