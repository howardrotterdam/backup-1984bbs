													<h2>美前司法部长要求旁听刘晓波庭审</h2>
						
						
																											<div id="postmessage_383601" class="t_msgfont">中国著名异议人士刘晓波被中国政府控以“煽动颠覆国家政权”案引起海内外各界关注。美国里根政府和布什政府时期的司法部长迪克索恩伯勒正寻求以观察员的身份旁听中国政府对刘晓波的庭审，而中国政府方面对此尚无回应。下面是自由亚洲电台记者寇天力的采访报道。<br />
&nbsp; &nbsp;<br />
<img src="http://boxun.com/hero/200912/xianzhang/xianzhang2009121901021.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
&nbsp; &nbsp;迪克索恩伯勒(本人授权)<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;迪克索恩伯勒是美国法律界的重要人物，曾任里根政府和布什政府时期的司法部长。今年8月，他就致信中国最高法院院长王胜俊，要求能以观察员的身份全程旁听中国政府对刘晓波案的庭审。他说：<br />
<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;“我认为这是个特别悲哀的案例，表明人们仅仅由于对人权问题坦诚说出心里话就会受到种种限制。根据对刘晓波的起诉罪名，刘晓波因参与零八宪章以及他有关民主人权和法治的文章而被控告。民主人权法治以及中国的司法制度等都是我长期感兴趣的领域，刘晓波一案对我来说很具代表性，因此我正寻求前往观察报道。”<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;刘晓波于去年12月零八宪章公布前夕被从家中带走拘押；直到今年的12月，被送往检察院正式起诉，罪名是“煽动颠覆国家政权”，理由是他所书写的一些文章以及参与起草零八宪章。该案在海内外各界都引起关注，并被认为是检验中国司法制度与人权保护的一块试金石。美国首都华盛顿的一家研究外交政策的非政府组织官员埃伦博克说：<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;<br />
<img src="http://boxun.com/hero/200912/xianzhang/xianzhang2009121901022.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
&nbsp; &nbsp;埃伦博克 （授权使用）<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;“你知道，刘晓波是中国最著名的异议人士，也是杰出的知识分子和活动人士。我认为中国政府已使他成了一个象征；中国政府依据他的作品及其参与零八宪章活动已决定以“煽动颠覆国家政权罪”起诉他，非常不幸，中国政府通过关押刘晓波、阻止零八宪章运动来显示其重要性，而我们正对此做出回应。刘晓波应该被释放，对于中国来说，他是个财富，他的作品反映了对个人、对人权的一种良知、一种责任，任何政府都应为此感到骄傲而不是进行打压。”<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;律师出身的索恩伯勒长期以来对中国的司法制度和发展予以关注，早年他还以私人身份访问中国，会晤当地的律师和法官，探讨法治等问题。针对刘晓波一案，他认为，这是外界审视中国司法透明度以及法治原则的一个机会。他说:<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;“这正是我所希望能发现的，因为从表面看来，对刘晓波的控告起诉似乎与法治并不一致，一个个人仅仅由于表达其观点就受到犯罪指控。当然，我 不想提前判断这一案例，我认为重点是，中国官员们同意我前往观察以推动中国司法制度的透明度，这对他们是有利的。”<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;鉴于过往中国政府对一些包括煽动颠覆罪在内的敏感案件都以闭门审理的方式进行，那么刘晓波一案，索恩伯勒是否能获得中国政府的允许前往观察呢？索恩伯勒认为，这种可能性还是有的:<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;“当然这取决于中国处理该案的官员和法官。到目前为止我对该案的研究显示它并没有涉及任何闭门审理所引用的国家秘密条款，而中国政府有关规定条款中清楚注明，所有的庭审应向公众公开。我的观点是，这个案子尤其应该向公众公开，这样才能让所有大众知道该案中进行犯罪起诉所运用的标准的必须透明度。”<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;不过，直到目前为止，索恩伯勒还没有收到中国方面的任何回应。但他并没有放弃，同时，他也要求美国政府提供帮助，美国国务院和美国驻华使馆已承诺将予以大力支持，索恩伯勒表示，允许他前往旁听观察，实际上对中国政府是有利的：<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;“我还没有收到任何回应，我已通过申请签证继续要求中国官方批准我前去旁听观察，我希望他们能明白，公开所有庭审并接受公众的审视，对他们是有利的。”<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;目前，索恩伯勒仍正继续努力，并等待中方的答复。<br />
