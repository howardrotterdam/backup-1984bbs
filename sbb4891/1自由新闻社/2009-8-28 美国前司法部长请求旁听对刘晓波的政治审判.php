																											<div id="postmessage_217515" class="t_msgfont">文章来源：维权网<br />
<br />
(2009年8月12日 周三)&nbsp;&nbsp;美国前司法部长迪克．松伯克先生请求中国当局给与他和其他法律同行旁听于2009年6月23日因为涉嫌煽动颠覆国家政权而被逮捕的知名作家和知识分子刘晓波的审判。<br />
<br />
在致中国最高法院院长王胜俊的信件中，松伯克先生说，“对我个人和许多跟进中国司法的法律界人士来说，刘晓波案件至关重要”。<br />
<br />
在另一个评论中，松伯克先生也提到了对公盟许志永先生的拘捕以及对那些接手人权案件的律师的打压。他说，作为期待中国法治进展的他和所有同行来说，许志永先生被拘捕和维权律师被剥夺律师执照，被骚扰甚至被关押等事件是值得严重关注的事件。<br />
<br />
刘晓波自从2008年12月8日《零八宪章》发表前夕，一直被拘押至今。《零八宪章》是一份要求在中国落实民主和人权的呼吁，在2008年12月10日正式发表。<br />
<br />
松伯克先生曾任里根政府和布什政府的司法部长，在1979年到1987年，担任过宾夕法尼亚州州州长，在1992年―1993年，是联合国负责行政和管理事务的副秘书长。<br />
<br />
信件原文：<br />
<br />
致：中华人民共和国最高法院院长王胜俊先生，<br />
<br />
抄送：中国驻美大使周文重先生<br />
<br />
尊敬的王胜俊先生：<br />
<br />
我为了自去年12月8日开始就被监视居住并在2009年6月23日因为涉嫌煽动颠覆政权罪被宣布逮捕的刘晓波先生的法律诉讼进展事情给您写信， 我理解有关刘晓波的调查还在进行中，迄今为止尚无判决。如果这个案件审判开庭，我希望能得到您的允许，和其他一些国际上的法律专家一起参与旁听。<br />
<br />
许多年以来，我个人都非常关注中国司法进展，我曾经以律师的私人身份访问过中国并和一些法官和律师见面，对他们的敬业精神我表示钦佩。对我个人和许多跟进中国司法的法律界人士来说，刘晓波案件至关重要，能亲眼目睹他案件的审判是我了解中国司法程序和见证刘晓波个人状态的一个机会。<br />
<br />
我个人简历如下，1988年―1991年之间，我曾任里根政府和布什政府的司法部长，我自从1958年开始开始从事律师实践，是宾夕法尼亚和哥伦比亚特区律师协会以及美国最高法院律师协会的成员。在1979年到1987年，我担任过宾夕法尼亚州州州长，在1992年―1993年，我是联合国负责行政和管理事务的副秘书长，我现在是在北京和上海拥有代表处的K&amp;L GATES 合伙律师行的律师。<br />
<br />
我希望您能惠予我的请求。我相信，如果刘晓波先生被审判，那能让国外的律师旁听审判，将有益正义。<br />
<br />
谨致!<br />
<br />
迪克.松伯克</div>

							
							
							
							
