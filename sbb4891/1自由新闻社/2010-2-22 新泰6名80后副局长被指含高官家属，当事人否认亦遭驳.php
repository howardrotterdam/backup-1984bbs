													<h2>新泰23岁女局长系当地组织部长儿媳</h2>
						
						
																											<div id="postmessage_490052" class="t_msgfont">23岁最嫩女局长系当地组织部长儿媳？<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; 作者：张洪峰<br />
<br />
名词解释： “外挂门” 外挂本是网络游戏中用于快速升级的一种非法网络工具，在本次事件中被网友形容为公务员任职时使用了“外挂”，导致升职速度超乎正常可以认知的程序。<br />
<br />
新闻背景：近日，网上出现一篇名为《令我十分震惊的任命公示》的帖子，发帖人“andybigfan”称，今年2月初，山东省新泰市（县级市）新提拔6名副局长，1 名法院副院长，这7人中，有6名是80年代出生，最年轻的只有23岁，“80后从此集体登上政坛”成为众多网友议论的焦点。昨日下午，新泰市委组织部一位葛姓负责人回应网友质疑称，这次领导干部的提拔符合相关规定。<br />
<br />
新泰市市委组织部葛姓负责人接受媒体采访称，这些新提拔的干部在原工作岗位都有突出表现，记者要求其列举时，葛又称，“不好说”。冯成昆(重庆商报)<br />
<br />
张洪峰评论：干部任前公示制度的好处就在于将干部任命放在阳光下晒，是“真能力”还是“靠关系”即可分出个一二来，新泰市大胆提拔年轻干部并无过错，只要经得起阳光自然是合理合情的，但面对媒体和网友的质疑，组织部的回应却是“不好说”，如果管干部任命的部门自己都对干部的工作能力“不好说”，说不出个所以然来，那又如何让公众信服呢？<br />
<br />
网闻综述：既然组织部回应“不好说”，那就需要探寻到底是有什么才“不好说”呢，根据在百度新泰贴吧网友的发言，加上我与当地一位消息人士的联系，综述下这个被当地网友称为“外挂门”事件中最受人关注副局长王然的传闻，仅仅23岁的新公务员被提拔为当地要害部门的副局长，面试成绩如此明显的高，此中奥妙无穷，其背景到底如何？<br />
王然，1986年7月出生，拟任新泰市国有资产管理局副局长。在新泰政府网站上找到的相关公告显示，王然参加此次考试的成绩为笔试64.83分、面试 86.00分、综合75.41分，而2008年，她参加公务员考试的成绩为笔试52.8分、面试79.2分、综合64.88分，两次考试的面试成绩都远高于笔试成绩。（官方资料）<br />
<br />
一 、王然是新泰市委组织部副部长王悦瑞的准儿媳（网传是儿媳，但据我与当地消息人士的联系，目前只是准儿媳状态）<br />
<br />
二、王悦瑞接近退休的年龄，兼任该市老干局局长，其对于干部任用是具有很大影响力的，王悦瑞的哥哥曾任该市机关工委书记，目前已经退休，但足见王家在该市具有一定的关系网。（网络消息）<br />
<br />
