<br />
任人唯亲 交叉提拔<br />
<br />
这种分赃式的政治具有严重排他性，大量制造社会不平等，也使很多优秀人才被卡，无法脱颖而出。表面看，官场很多政治任命，无论程序还是资历都似无懈可击，但实际上背后都有文章。很多官员以交叉提拔的方式逃避外界监督。最简单的是，你提拔我儿子，我提拔你女儿，共同进步，皆大欢喜。<br />
