富二代飚车斗富　官二代卡位争官<br />
<br />
权力世袭已成中国特色的政治文化。山东新泰市新提拔六名副局长、一名法院副院长，这七人中有六名是八十年代出生，最年轻的只有二十三岁。当绝大部分同龄人都为一口饭而奋斗时，官二代们却卡位争官。中国社会的不平等，由此可见一斑。<br />
<br />
据内地媒体报道，这些新贵选拔虽然是在公开公正的名义下进行，但所谓的公开选拔成了一些人任人唯亲的遮羞布。奥妙在于这些新贵的笔试成绩虽然比较低，但面试成绩却比别人要高出一大截。由于面试成绩操纵在与这些新贵有千丝万缕关系的叔叔伯伯手中，平民子弟即使满腹经纶，也只能望衙门兴叹。<br />
<br />
类似事件近年并不罕见，河南固始县去年选拔正科级和县局级官员任用，所有「公开」选拔的乡镇长官员，全是官宦子女和当地两大房地产老板的亲戚。辽宁本溪提拔三名团市委正副书记，被选上者都是当地市委常委的子女，有的甚至刚刚从国外读完「野鸡」大学，急着赶回国争权卡位。<br />
<br />
