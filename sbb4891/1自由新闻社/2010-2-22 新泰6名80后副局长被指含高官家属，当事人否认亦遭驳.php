我们先看这一段“今年2月初，山东省新泰市（县级市）新提拔6名副局长，1名法院副院长，这7人中，有6名是80年代出生，最年轻的只有23岁……网友发现，最年轻的副局长王然2008年8月30日才通过公务员考试进入新泰市纪委工作，担任案件审理室科员不足2年。”我们再看看中央这个规定&lt;a href=&quot; <a href="http://www.gov.cn/gzdt/2008-12/08/content_1171860.htm" target="_blank">http://www.gov.cn/gzdt/2008-12/08/content_1171860.htm</a>&quot;&gt;“关于印发《公务员职务任免与职务升降规定（试行）》的通知”&lt;/a&gt;。规定中有这样两句话“（三）晋升乡科级副职领导职务的，应当担任科员级职务三年以上”;“（六）晋升副主任科员职务，应当任科员三年以上；（七）晋升科员职务，应当任办事员三年以上。”，新泰几个80后的，基本上都不符合这个规定。所以就等新泰把中组部和人保部刚刚发布的这个最新规定就给废了。　　<br />
<br />
　 　<br />
<br />
我们再看看这一段“昨日下午，记者联系到新泰市市委组织部，一位葛姓负责表示，此次干部公开选拔的笔试是由山东省统一出题，新泰市组织的考试，面试则以面谈为主，提拔的程序符合相关规定。”注意这个关键点“面试则以面谈为主”，这个做法符合中央文件规定吗。我们从&lt;a href=&quot; <a href="http://www.hydj.gov.cn/htdocs/XXLR1.ASP?ID=7088" target="_blank">http://www.hydj.gov.cn/htdocs/XXLR1.ASP?ID=7088</a> &quot;&gt;“中共中央办公厅关于印发《公开选拔党政领导干部工作暂行规定》等五个法规文件的通知”&lt;/a&gt;看到如下内容　　<br />
<br />
“第二十条&nbsp;&nbsp;面试由面试小组负责考试和评分。面试小组由有关领导、专家、组织人事干部等人员组成，一般不少于7人。 同一职位的面试一般由同一面试小组负责考试和评分。第二十一条&nbsp;&nbsp;面试小组成员应当具有较高的思想政治素质，公道正派，并熟悉人才测评工作。面试小组中必须有熟悉选拔职位业务的人员。面试小组成员要实行回避制度。面试前应当对面试小组成员进行培训。”　 　<br />
<br />
&nbsp; &nbsp; 面谈和面试是两个完全不同的概念，面试要专家出题，具体面试时有很多细致的规定。而面谈很简单了，赵书记的女儿由钱县长主持面谈，钱县长的侄子由赵书记主持面谈，题目既可以事先定好由领导们内部沟通好，也以那孩子熟悉什么谈什么，旁边再陪上两个组织部的人以示公正。当然这样操作面谈成绩就远远高于笔试成绩了。所以他们采取偷梁换柱的办法，就又破了中央另外一个规定。　　<br />
<br />
　 　<br />
<br />
　　难道仅仅山东新泰是这样吗。其实不止是山东，很有些地方也是这样的。“辽宁本溪市选拔团干部结果被宣布无效......前不久本溪市通过“双推双考”选拔的４名团干部，由于个别环节不符合有关规定，经本溪市委研究决定，此次选拔结果无效…… 公选4名团书记 3人是市领导子女。”此外，还有江苏泰州25岁的副处，湖北那个著名的清华毕业的29岁的市长等等。　　<br />
<br />
　 　<br />
<br />
　　当然主要是山东有前科。那个“我很务实”的那个28岁副厅级干部，省团委副书记　张辉　先生大家还记得吧。这小朋友应当30了，两三年副厅了，该扶正了吧。再转上三五个岗位，特别是任一下地市级的一把手。该省级了，预计10年后……想一想都使老拙口水直流啊。山东的同志还是特别敢于解放思想的，前面有28岁的副厅，现在这副厅的“女儿”、“儿子”们就纷纷出来了。前头有车，后面有辙嘛，现在克隆技术这么发达，这不，一下子就由一个克隆了六个　　<br />
<br />
　 　<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;而新泰市的官员说“这次领导干部的提拔符合相关规定”，我不知道符合什么规定，是新泰的土政策威力大呢，还是中央规定的威力大呢？是规定互相打架呢，还是不同的规定适用于不同的人，特别是爹或妈不同的人！　　<br />
<br />
　 　<br />
<br />
　　那么新泰这种做法公正吗？公正不公正，还是新泰的干部说了算。我们再看看来自新泰政府门户网站的《新泰市公开选拔领导干部拟任人选公示》中的唯一一个“另类”“张圣银，山东费县人，1976年3月出生，研究生，工学硕士，现任潍坊滨海经济开发区交通局基建科办事员，拟任新泰市建设局副局长。” <br />
<br />
　　我们看看陪绑的张圣银小朋友（一群人因为有了他才显得“公正”嘛），这肯定是家里没有背景的。这是一个研究学历的高学历人才，进入公务员队伍后，居然要从办事员干起，参加工作也近十年了吧，居然连科员也不是，他是按正常升职的规定走的，也34岁才能任副科。当然肯定是他家贫寒，上面没人，即使研究生学历，工作后可直接任副科，网友们跟贴中讲了有这个规定，但从办事员干起，干了近十年！和一群比他年轻的十岁的同志一同提拔，张小朋友不知道心里会是个什么想法。新泰市其他的同志呢？　　<br />
<br />
　　　　<br />
<br />
　　干部制度是一切政治制度中的核心制度，现在这个制度，却被各地方随意地破坏着。破坏的原因很多很多。后果呢？往小里说，很多干部对这些已经麻木，完全“被丧失”了理想信念，完全被抑郁了。住大里说呢？　　<br />
<br />
　 　<br />
<br />
　　根据笔者多年的观察，这样快速成长起来的领导干部，多斗是干部子女，是中高级干部的子女，有的是家里很有钱或父母亲关系很硬的。干部制度，对于他们来讲，根本不起任何约束作用。而通过这种途径提拔起来的干部，只是政客，很少产生政治家。往往说起来非常漂亮非常动听，做起来束手无策，特别是对那些多年在官场混的老油子。最后他们以路易十六洪水淘天的不负责任的态度，“和谐”地和那些老油子一同混日子，制造着官场的奢华和空转，任由这个社会烂下去！　　<br />
<br />
　 　<br />
<br />
　　一些地方为什么要这样搞？有的网友一针见血，很简单，为了给本省培养十年二十年后的高级领导人后备人选呀。问题是这种现象开始在全国各地蔓延，大批的这种一路顺风的干部，没有在大风大浪里锻炼过，没有经过风雨，见过世面，只经历过官场风雨，见识过洋世面，长大后，能够任凭风浪起、稳坐钓鱼船吗？和平演变我们的第三代第四代过得硬，照样的选拔路线，第五代第六代能过得硬呢？　<br />
