																											<div id="postmessage_487845" class="t_msgfont">23岁女科员升副局长受质疑<br />
<br />
&nbsp; &nbsp; 近日，网上出现一篇名为《令我十分震惊的任命公示》的帖子，发帖人“andybigfan”称，今年2月初，山东省新泰市（县级市）新提拔6名副局长，1名法院副院长，这7人中，有6名是80年代出生，最年轻的只有23岁，“80后从此集体登上政坛”成为众多网友议论的焦点。昨日下午，新泰市委组织部一位葛姓负责人回应网友质疑称，这次领导干部的提拔符合相关规定。<br />
<br />
&nbsp; &nbsp; <br />
&nbsp;&nbsp;7个副局长 6人是80后<br />
<br />
&nbsp; &nbsp; 网友“andybigfan”在网上的帖子中展示了一份名为《新泰市公开选拔领导干部拟任人选公示》，称在公开报名、资格审查、统一考试、组织考察基础上，经新泰市委研究，拟任用7名同志担任副局级领导职务。<br />
<br />
&nbsp; &nbsp; 网友发现，最年轻的副局长王然2008年8月30日才通过公务员考试进入新泰市纪委工作，担任案件审理室科员不足2年。<br />
<br />
&nbsp; &nbsp; 记者登陆新泰市政府网站，找到了这份公示，拟任的7位副局长或副院长有6名是80后，原职务多为办事员、书记员、科员等基层职务，拟出任法院副院长或规划局、审计局、司法局、国有资产管理局的副局长。其中，年龄最小的是王然，女，山东新泰人，1986年7月出生，大学学历，管理学学士、经济学学士。<br />
<br />
&nbsp; &nbsp; 面试成绩远高于笔试<br />
<br />
&nbsp; &nbsp; 记者在新泰政府网站上找到的相关公告显示，王然参加此次考试的成绩为笔试64.83分、面试86.00分、综合75.41分，而2008年，她参加公务员考试的成绩为笔试52.8分、面试79.2分、综合64.88，两次考试的面试成绩都远高于笔试成绩。<br />
<br />
&nbsp; &nbsp; 昨日下午，记者致电新泰市委组织部，希望能采访到这6名80后，一位葛姓负责人表示必须请示领导，截至记者19:00发稿时，仍未收到答复。记者致电到最年轻的副局长王然所在的新泰市国有资产管理局，工作人员称其还没有上班，并不了解此人。<br />
<br />
&nbsp; &nbsp; 网友质疑<br />
<br />
&nbsp; &nbsp; 1　经验少能否胜任？<br />
<br />
&nbsp; &nbsp; 网友“guaixiaobu”称，新泰市是全国百强县第25位，一次性提拔这么多大学毕业才几年的年轻领导，也升得太快了，经验少阅历浅，是否能胜任？为何不先提起来当主任，多积累点工作经验？<br />
<br />
&nbsp; &nbsp; 昨日下午，记者联系到新泰市市委组织部，一位葛姓负责表示，此次干部公开选拔的笔试是由山东省统一出题，新泰市组织的考试，面试则以面谈为主，&nbsp;&nbsp;<br />
提拔的程序符合相关规定。这么年轻，是否能胜任呢？葛称：“不好说”。<br />
<br />
&nbsp; &nbsp; 2　助理审判员直接任法院副院长<br />
<br />
&nbsp; &nbsp; 在公示中，原岱岳区人民法院立案庭助理审判员刘婷婷，才25岁，拟任新泰市人民法院副院长，引起了众多网友关注。网友“ljs77588”说助理审判员应该是法院里没有通过司法考试的工作人员，不能单独办案，当副院长是不是违反《法官法》了？按照规定，人民法院的院长、副院长应当从法官或者其他具备法官条件的人员中择优提出人选。<br />
<br />
&nbsp; &nbsp; 新泰市市委组织部葛姓负责人解释，刘婷婷已经通过了司法考试，具备资格。<br />
<br />
&nbsp; &nbsp; 3　刚过试用期就升副局长?<br />
<br />
&nbsp; &nbsp; 年仅23岁的王然担任国有资产管理局副局长成为质疑的焦点。<br />
<br />
&nbsp; &nbsp; 网友“在家过晚年”说，“一般人22岁大学毕业，就算当年直接考上公务员，1年多能出什么成绩？有重大发明？群众极度拥护？想不通啊，1年多也就刚刚熟悉岗位而已，马上就飞奔副局长了，想不通啊！”网友“变色蜥蜴”称，刚过一年试用期就升任副局长，这么短的工作经验是否能胜任重要职务？”<br />
<br />
&nbsp; &nbsp; 对此，葛称，这些新提拔的干部在原工作岗位都有突出表现，记者要求其列举时，葛又称，“不好说”。<br />
