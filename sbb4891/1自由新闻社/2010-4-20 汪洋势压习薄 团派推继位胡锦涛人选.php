													<h2>汪洋势压习薄 团派推继位胡锦涛人选</h2>
						
						
																											<div id="postmessage_572695" class="t_msgfont">最新一期香港《动向》杂志来自北京高层的可靠消息表明：广东省委书记汪洋成为团派推出的继任胡锦涛总书记之位的新人选，李克强已经放弃了总书记职务的竞争，转而提前M入总理角色。而江泽民派系推出的习近平、薄熙来，由于江系欠下的法轮功血债而前景不妙。<br />
<br />
最新4月号《动向》披露：为了让李克强在未来与总书记高度分权，胡锦涛新近签署的《国防动员法》赋予了总理会同中央军委调动军队的权力。李克强将是第一个上任就享有调兵权的政府首脑。团派再次赢得了十八大权力布局的先机。<br />
<br />
全国人大常委会2月26号通过首部《国防动员法》。观察家认为，这是鉴于前年四川大地震时温家宝调动军队救灾不灵而在体制上做出的转变。<br />
<br />
今年2月初全国省部级干部会议上，胡锦涛发表重要讲话强调加快经济发展方式转变，之后没有安排习近平作第二主讲，而是让李克强主讲部署具体方略。薄熙来甚至缺席会议。<br />
<br />
美国哥伦比亚大学政治学博士王军涛对此指出，胡锦涛核心传递的信息明显无误：习近平的候选核心地位已被废除。<br />
<br />
《动向》杂志表示：汪洋在两会期间的高调使得他作为总书记候选人的态势展露无疑。北京观察家们发现：在胡锦涛为两会定下「少宣传领导」的调子之后，习近平几乎没有发表言说，而汪洋却被人民网长篇大论地吹捧了一番。<br />
<br />
团派在人民网披露了当年南巡的邓小平召见汪洋的细节，此前18年从未报导过。当时，30多岁的汪洋在安徽铜陵的改革，招致了中央保守势力的反对，而邓小平十分赏识汪洋的才干，前总理朱g基也说他「年纪不大、胆子不小」。<br />
<br />
《动向》杂志引述一位退休官员透露说：「邓看出江泽民是个『不成气的东西』，提前敲定胡锦涛接班并给胡勾画团派班底。」汪洋、李克强都是胡锦涛的安徽老乡兼团派亲信。<br />
<br />
针对江泽民的「三个代表」，胡锦涛在十七大提出了科学发展观，希望代替现有的高能耗、高投资、低效率的以出口为主，而牺牲环保、牺牲工人农民利益的经济模式。<br />
<br />
时政评论家陈破空指出：江泽民所坚持的，是「让少数人先富起来」，而胡锦涛所强调的，则是「走共同富裕道路」。<br />
<br />
汪洋2007年底调任广东省委书记，替胡锦涛接收江系盘踞的广东。汪洋在广东发起新一轮思想解放，推行经济和行政改革，试图以科学发展观取代「三个代表」。<br />
<br />
《中国事务》主编伍凡对希望之声国际广播电台说：「胡锦涛他企图在广东依靠汪洋来提出一个腾笼换鸟的经济政策，希望改造发展一个科学发展观能够照顾到环境污染和百姓生活的这么一个发展路线。可是在广东受到了极大的阻碍，这也是促发了胡锦涛下定决心要收拾广东帮。」<br />
<br />
藉着胡锦涛的支持，汪洋在广东清洗了江系腐败高官，包括广东省政协主席陈绍基、前省纪委书记王华元、深圳市长许宗衡等人，以及前中国首富黄光裕案所牵涉的郑少东、黄松有等周永康人马。<br />
<br />
因追随江泽民迫害法轮功，陈绍基、王华元、周永康，以及针对汪洋唱红打黑的薄熙来，分别被法轮功学员在海外以酷刑及群体灭绝罪起诉，或被追查国际组织通告追查罪行。<br />
<br />
《动向》杂志披露：江系人马自知在政法系统作恶太多，担心薄熙来日后藉清洗政法系统而捞取政治资本，不惜派出政治局常委周永康向薄熙来示好。而薄熙来目前已在针对汪洋、贺国强、习近平的内斗中失势。<br />
<br />
前《世界经济导报》驻北京办事处主任张伟国对自由亚洲电台说：「最近这段时间又处在十八大之前，再加上薄熙来在重庆那边用毛的那种腐旧的东西咄咄逼人，汪洋作为团派的这样一个封疆大吏，他们之间的较量倒是有点刀光剑影的味道。」<br />
<br />
《动向》杂志观察到：今年两会期间，习近平没有发表新论调，新华社所做的十几个焦点系列报导里面，涉及政治局委员的只有温家宝、汪洋、薄熙来三人。习近平的形象顿显黯然失色。<br />
<br />
《动向》杂志表示：汪洋被宣传成邓小平改革开放政策的实践者，牢牢地靠住了邓钦点胡锦涛的法统体系。而习近平除了是个太子党的人选，是江泽民硬塞给胡的接班人之外，没有其他能说服社会的任何资格。<br />
<br />
北京政治分析人士指出：习近平如果不能摆脱江系色彩，继续与胡温团派为敌，有可能成为江泽民的政治殉葬品。</div>

							
							
							
							
