																											<div id="postmessage_709046" class="t_msgfont">这是他校内上的一篇日志<br />
我本是个后知后觉的人，对于媒体的覆盖倒能淡然处之，直到我看到了许多网站对于我申请失败的分析。我忍不住了。<br />
<br />
主要不是要为自己正名，而是要向大家向社会特别是向学弟学妹们传达一个简单而深刻的道理：一切靠自己。<br />
<br />
有某位留学公司的执行长认为我的申请一定是有斧凿之痕，只展示了各种头衔和奖项，而忽视了自己内心中“最真诚、最有兴趣的部分”，从而导致失败。也有人评论道美国人觉得我的学生会主席和班长的工作经历都是官方行为，在“政审”方面没有过关。还有朋友直接评论道：“书呆子，活该！”<br />
<br />
感谢他们的精彩点评。如果把我的申请失利作为一个案例研究而呈现于大学课堂之上，他们的解答不可谓没有创意，不可谓不深刻。然而，我想，真正了解我的人是我自己，真正能够跟大家分享我的经验的人，还是我自己。所以我想在此回驳以上观点，并提出我自己的感悟。当然，回驳并不是冒犯；观点的碰撞才最能营造学术的氛围。况且，我还要感怀所有对此发表见解的人（无论什么见解），因为毕竟，你们对我的关注，见证了我的成长。<br />
<br />
回应1：我的申请中集中突出的是我的好奇心，而非各种头衔。我的essay中，完全没有提我的各种头衔，完全没有提到数学竞赛的奖项。我的主题全部是兴趣，特别是对于这个世界、对于生活的兴趣，对于文化和文化交流的兴趣。我用了大量的心理描写，刻画了自己的心路历程，并且最终落在了I love everything I do, and do everything I love的主题上面。<br />
<br />
回应2：其实，学生会和班长的工作并不是我的侧重。恰恰相反，我的申请中着重于展现在模联、志愿服务和宿舍管理方面的initiative和creativity. 特别是在面试过程中，我强调的是我的自己联系、创办的活动和机会，比如校内外的志愿服务、北京地球村的环保行动等等，避开了主流的“官方”行为。我想，如果要吸引对方的注意，就要尽量“笔落惊风雨，诗成泣鬼神”。<br />
<br />
回应3：这个我没话说，不过我希望能通过自己的实际行动证明，高考状元不一定（或者说一定不）是书呆子。至少，他们还有丰富的感情生活（这个略去）。<br />
<br />
 <br />
<br />
那么到底是什么原因使我的申请出现了问题呢？或许只有美国人自己知道，而他们又从来都不会告诉你。那么我想，现在我自己的分析，应该是最权威的了。既然权威，就要深入，我们说得远一点、广一点。<br />
<br />
 <br />
<br />
外因：美国（人）的角度<br />
<br />
曾经有人安慰我，如果你想被美国名校录取，要么你家有钱，要么你家有人，要么你是女生。这当然有些言过其实，不过它传达的精神或许值得我们品玩。<br />
<br />
 <br />
<br />
内因：我自己的问题。<br />
<br />
1、&nbsp;&nbsp;起步晚：我做出申请美国本科的决定，是在高二上学期学生会竞选获胜、丘成桐杯成绩差强人意之时。反观成功的同龄人，这个时间或许可以提前5年甚至15年。我对美国的认识相当有限，自然对于美国人的胃口把握也就不很到位。<br />
<br />
2、&nbsp;&nbsp;SAT分数不够高。其实2240还算不错了，但是在美国人非常重视的CR和Essay部分我只有710和11分，当然更好的分数才配得上自己的志向。SAT2只有传统的数理化，而且没有AP（当然这个的重要性没有那么大）。这与自己起步晚、活动多而分散了一定经历有关、<br />
<br />
3、&nbsp;&nbsp;美式竞赛AMC-AIME系统成绩不算最好。虽然在高中联赛-集训队系统中我取得的成绩算是申请人中最为出色的之一了，但是（我自己也奇怪）AMC和AIME的成绩至少没有拿到过学校最高分，而美国人当然相信自己的系统。这完全是我的问题，AMC和AIME考得太不在意了。<br />
<br />
4、&nbsp;&nbsp;不了解很多讯息和包装手段，包括被waitlist之后还可以用被录同学的推荐信这样高级的手法……<br />
<br />
5、&nbsp;&nbsp;过于全面的展现了自己。我感觉能写的太多了，最后全面照顾的结果就相当于什么都没有照顾到。如果你每方面都很优秀、都很喜爱，美国人是不会相信的（这或许是为什么有人会以为我的申请有斧凿之痕）。技巧是，一两个闪光点远远胜过几百个闪光点，因为你能被阅读者记住。<br />
<br />
6、&nbsp;&nbsp;（我不觉得这是我的问题）我全部要了全奖，税单的收入少的可怜(from an American perspective)。<br />
<br />
7、 …………原因的分析可以继续，但是我不承认“自负”是一条。<br />
<br />
 <br />
<br />
 <br />
<br />
最后说两句<br />
<br />
从留学申请道内人士角度看，我是个彻头彻尾的失败者。Brown, Caltech, Columbia, Duke, Harvard, MIT, Princeton, Stanford, University of Chicago, Williams, Yale 这十一个顶尖学校全部拒绝了我（虽然其中H/Y/M/W/Columbia/D/B都让我有过waitlist的幻想），这或许在中国人留美申请的历史上也可以记上一笔。但是我觉得这些拒绝并不能摧垮我对自己、对朋友、对这个世界的信任，因为毕竟，申请只是一个经历，申请结果也只发生在过去。美国人对大陆学生的录取现在看已经有了一套比较有章可循的模式，因为大量留学机构的介入使得在中国学生和美国大学的双向选择中很多心照不宣的传统已经形成。我觉得我最成功的一点，在于自己敢于申请全奖，敢于抵制中介，敢于面对挫折。我还坚守着一个真实的、没有被“专业人士”的咨询打乱的自我，纵然这个自我并不完美，但他很纯粹，让我很踏实。<br />
<br />
当然，我要对学弟学妹们说，选择中介而走入世界顶尖大学亦无可厚非，这是灵活变通之道，而对于你们的未来或也大有裨益。但是，不管你选择了怎样的道路，请你热爱自己的选择，然后全身心投入去做，对于结果保持一个开放的心态。申请的作用在于使你认识自己，结果完全应该是第二位的考量。纵使中介帮助了你，也请你一定要做自己的主人，在自己的展示过程中扮演主动。功利一点不一定不好，有人引着浏览身边的风景，也不一定不从容淡定。但是，我们一定不能忘了回家的路。<br />
<br />
最后分享两句话：<br />
<br />
1.&nbsp; &nbsp;&nbsp; &nbsp; It’s better to be rejected by Princeton than by yourself.<br />
<br />
2.&nbsp; &nbsp;&nbsp; &nbsp; 1+1在Harvard等于2，在清华还是等于2。本科教育的优劣我们都没有发言权。与其争论美国二流大学和国内顶尖高校哪个好，不如下定决心自己对未来的国内外教育理念做出革新。<br />
<br />
 <br />
<br />
祝大家都能快活地为自己的前程奔波，能始终不忘自己存在的价值和奋进的动力。我们一定会成功！<br />
