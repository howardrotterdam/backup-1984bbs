北京高考状元被美国名校拒绝的背后<br />
[ 2010-6-27 11:00:00 | By: 何也然 ]<br />
<br />
 陈九<br />
<br />
人大附中是我的母校，她今年出的高考状元李泰伯，总分达七百零三分，而且是班长，学生会主席，校模拟联合国主席，三次全国数学竞赛冠军，这样一个德智体全面发展的学生，在申请美国十一所前二十名大学时，居然全部被拒，令舆论哗然。这究竟为什么，这究竟说明什么呢？<br />
<br />
先看学校，十一所大学整齐划一全部拒绝，这本身很不正常。像李泰伯这样出众的学生，即便按美国大学录取标准，不光看成绩，更看综合素质，李的条件也是优秀的，李在社会活动方面的突出表现，甚至强过学业，一所著名中学的学生会主席，全校仅此一人，独居领导地位，这还不能说明他的综合素质？如果是美国一所高中的学生会主席，绝对是申请名牌大学的竞争优势，纽约两所最著名的公立高中，一所叫亨特高中，一所叫史代文森高中，每年毫无例外，学生会主席必被前十名大学录取。怎么到李泰伯这儿，就整齐划一地不灵了？答案很简单，这些大学必定对中国学生的申请，另有一套整齐划一的标准。<br />
<br />
是什么标准呢？学校肯定不会说，我们只能用推测法，通过周边分析来判断该标准到底是什么？首先，让我们听听北京学美留学公司执行长张恒瑞针对李泰伯被拒一事说的一席话：“对美国名校来说，他们更看重学生内心世界里最真诚、最有兴趣的部分。美国学校会看学生是否真心投入某件有意义的事，而不是有计划有目的性的参加。美国名校对看得出人工操作的痕迹者，都不会录取。”<br />
<br />
张执行长这段话可谓玄妙深远。内心世界，真心投入？这听上去看不见摸不着，如何衡量呢？如果美国大学真敢以此为由拒绝一名本地学生，这学生就敢把学校告上法庭，因为这涉嫌人格歧视。你既然谈的是心，是人格，那你凭什么说我不真诚，有人工操作痕迹？这个官司学校打不赢。因此，美国学校对中国学生之所以敢采取人格标准，而且是个超学术的政治标准，只能说明它们对中国学生的衡量有其政治考量，有其政审的要求，这个要求可能并非来自学校，而是来自某个政治机构，所以整齐划一就毫不奇怪了。<br />
<br />
<br />
现在再看看李泰伯的个人经历，他的社会活动均在官方背景之下，起码西方人会这么认为，班长，学生会主席，都是官方体系的一部分，这样成长的孩子显然不符合西方的自由主义标准，政治上不可靠，这才是李泰伯被拒的原因。美国学校对中国学生搞‘政审’，这就是李泰伯被拒事件的深刻含义。<br />
<br />
不信吗？没关系，今后同类事件一定还会发生。整齐划一的拒绝一定不是偶然的，因为偶然解释不了这个整齐划一。<br />
