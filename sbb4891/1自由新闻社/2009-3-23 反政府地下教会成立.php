													<h2>反政府地下教会成立</h2>
						
						
																											<div id="postmessage_59344" class="t_msgfont"><a href="http://www.wyzxsx.com/Article/Class22/200903/75306.html" target="_blank">http://www.wyzxsx.com/Article/Class22/200903/75306.html</a><br />
郑州市毛泽东思想学习会成立<br />
作者：英剑侠&nbsp; &nbsp; 文章发于：乌有之乡 <br />
<br />
2009年3月21日，郑州市各界群众自发汇聚一起，共议组织起来学习掌握宣传运用毛泽东思想事宜。　　<br />
<br />
同志们各自带来了珍藏的毛主席著作，有四卷本、合订本、《毛主席语录》、《毛主席的五篇哲学著作》、无产阶级专政下继续革命的理论文章。大家相互交流传看，爱不释手。人人胸前佩戴着毛主席像章，像一团团热情的火，映得会场烘热一片。　　<br />
<br />
嘹亮的《东方红》歌曲拉开了酝酿序幕。老、中、青同志争相发言。　　<br />
<br />
上街区的一位工人同志说：“为什么我们要组织起来学习毛泽东思想，因为现实对我们的教育太大了，还因为我们与毛主席血肉相连。<strong>反动的东西长期存在，假的东西迷惑了人民三十年，这些反动的东西长期存在，天理不容。这需要我们拿起毛泽东思想武器，戳穿它、战胜它，我们不能死等，不能抱幻想，把革命事业断送，要抓紧时间把毛泽东思想传承下去，是当务之急。我们要做播种机。</strong>”　　<br />
<br />
一位女同志动情地介绍了她长年坚持学毛著的情况，七六年毛主席逝世一段时间后，她擦干了眼泪，就起上了自觉宣传毛泽东思想的征途，逢人就讲，无处不宣。厂里的头头儿偷卖设备，她带领大家揭露他们，说毛主席活着吓死他们也不敢这样做。厂长说，毛主席不是死了吗？她说，毛主席死了，他的思想不会死！热爱他的人民永远在！吓得几个头头儿面无人色。毛泽东思想在她心中就是一团火，她要燃烧，她说她人老心不老，活一天宣传一天，要毛泽东思想传承下去。文化低不怕，克服困难学网文，<strong>制作宣传页，制作光盘，到群众中去宣传！</strong>　　<br />
<br />
一位长期在省文化宫做宣传的男同志介绍了文化宫工人同志们持之以恒宣传毛泽东思想，又介绍了自己和周围的同志们是如何在日常生活中去维护毛主席的，他自己就从2001年至今一天不拉地佩戴毛主席像章。还讲了在日常中如遇到污蔑、造谣、诽谤毛主席的言论要大胆地揭露、发动群众，打电话报警。他还讲述了纺织系统的工人用毛泽东思想捍卫国企的壮举。针对有些同志有怕的情绪，他大义凛然地说，怕什么，我们从事的是天底下最光荣、最阳光、最光明正大的正事，反动的东西们最害怕人民说拥护毛主席，最害怕群众团结跟他们斗，他们色厉内茬。比如早在2001年，我们在毛泽东广场纪念毛主席，有警车便衣警犬陪伴，拉着警戒线都没有挡住我们，<strong>四君子还因此坐了法西斯监狱</strong>，这些都没有吓倒我们，形成了今天更多更普遍的毛泽东纪念热潮。反毛干将李锐在郑州还不是被斗得屁混尿流，这些都是人民群众活学活用毛泽东思想的生动例证。　　<br />
<br />
不少同志发言说，要求组织起来学习毛泽东思想的群众真是太多了，我们怎样联系他们，团结他们，现在我们找到了一个很好的方法：组织起来，通过学习，就能把大家凝聚在一起。　　<br />
<br />
另外，学习要带着问题学，学了就要用。如国有企业是如何被瓜分的，私有化形成的根本原因，我们为什么从主人变为奴隶等，都要有针对性地在学习中弄明白，然后才知道怎么去做。　　<br />
<br />
老同志们建议，学习尤要吸纳年轻人参加，起到重要的传承作用，年轻的同志们表示，要走与工农相组合的道路。　　<br />
<br />
热烈的发言使每一个同志都感受到学习毛泽东思想的热潮就要兴起，毛泽东思想的春天就要来到。　　<br />
<br />
本着自发、自愿、自由结合、老中青结合、不拘形式、集中辅导、分组讨论、灵活方便的学习原则，同志们当场组成了十数个学习小组，并表示相互联系、相互学习、互补长短、学用结合。　　<br />
<br />
在热烈的掌声中，郑州市毛泽东思想学习会诞生了！　　<br />
<br />
同志们用高昂的《国际歌》来庆祝学习会的成立。　</div>

							
							
							
							
