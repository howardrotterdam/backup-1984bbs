毛主席指出：“代表先进阶级的正确思想，一旦被群众掌握，就会变成改造社会、改造世界的物质力量。”我们掌握这个武器的目的，就是要“变成改造社会、改造世界的物质力量”，我们我们掌握这个武器的目的，不是要弹劾反革命政府，而是要推翻反革命政府。 <br />
用毛泽东主义武装人民的思想，我们的斗争就必然胜利，不用毛泽东主义武装人民的思想，我们的一切革命斗争就只能停留在肤浅的表面和口头上而不能取得胜利。所以，把毛泽东主义这个锐利武器发放的人民群众手中，利用一切可以利用的形式，采取一切可以采取的办法，把深揭狠批统治集团的檄文和我党的主张传递到全国各地，带领人民群众共同向统治集团发起全面的、持久的、猛烈的、一波又一波的进攻是每一个党员和已经在思想上入了党的革命积极分子的光荣任务！ <br />
<br />
八、扫除左派一切错误观点，为无产阶级反复辟大革命运动开辟道路的声明 <br />
<br />
如何对付资本主义的大复辟，左派从来就争执不下，长期以来对解决复辟问题，始终是处在对“杀猪杀屁股，各有各的杀法”的“杀法”研究上。诸如什么“重上井冈山，建立根据地，武装夺取政权”，什么“对于资本主义复辟当局的斗争已经不是“反对修正主义”问题，而是与官僚资本主义的斗争”，什么“复辟已经完成了，现在的革命就不是反复辟了”，什么“我们进行的革命应该是民主主义革命”，什么“到农村去，走集体主义道路”，什么“到基层去交朋友，一变二、二变四、四变八……”，什么“展开合法斗争，促使中央左转”，什么“武装割据，攻城略地”，等等等等，革命策略可谓让人眼花缭乱、不胜枚举。 <br />
所有这些观点虽然在愿望上是好的，但实际上却是片面的、僵死的、形而上学的。有的是从根本上否认中国共产党修正主义叛徒统治集团的客观存在；有的是对现代修正主义者的复辟手段视而不见；有的是片面强调某一点而忽略本质和主体；有的是根本就不懂得阶级性质和阶级斗争。正如列宁指出的那样：“他们都自命为马克思主义者，但是对马克思主义的了解却迂腐到了极点。马克思主义中有决定意义的东西，即马克思主义的革命辩证法，他们一点也不懂得。”他们甚至不能理解毛主席“代表先进阶级的正确思想，一旦被群众掌握，就会变成改造社会、改造世界的物质力量”的伟大教导是什么意思。更有甚者连这次革命的性质都弄不清楚，这岂不是十分荒唐和可笑吗？ <br />
&nbsp; &nbsp; 根据毛泽东主义的道理，马克思主义活的灵魂在于具体的情况作具体分析，这句话会说的人很多，真正会应用和发挥的人却极少。机会主义和教条主义者就是把马列毛主义的理论当作抽象的、狭隘的公式到处生搬硬套，他们拒绝研究矛盾的变化和矛盾的特殊性，具体讲就是不研究中国官僚资本主义是怎样演变的，不承认中国官僚资本主义的特殊性是修正主义形式的。只看现象，不看本质。只抓一点，不及其余。只顾历史经验，从不应变创新。看见是个瘸子便要去治人家的腿，却不知是由中风而起，不知中风不愈，其腿永瘸的道理。所以，呼喊着重上井冈山的，至今山上空无一人，嚷嚷着去基层“变”朋友的，至今也未能“变”出个名堂，走集体道路的南街村倒是名声在外，却撼不动统治集团一根毫毛，挡不住大复辟一泻千里。凡此种种，最终也只能是“躲躲闪闪，就像猫儿围着热饭兜圈子。（列宁语）” <br />
修正主义叛徒统治集团与官僚资本主义的关系是因果的关系。“因”的问题迎刃而解了，“果”的处理也就势如破竹了。如果对“因”没有正确认识，对“果”也就无从下手。所以，如果我们直接处理“果”而不察其“因”，不仅不能从一团乱线中抽出它的头来，而且也不符合事物发展的客观规律。所以，轻视修正主义统治下的革命理论斗争是极其错误的，是严重违背马克思主义的革命辩证法的。 <br />
另外，政治上的折衷主义、思想上的自由主义、理论上的教条主义、形式上的机会主义、行动上的山头主义等等，都是横在中国无产阶级反复辟大革命运动前面的绊脚石。这些障碍和那些错误观点，实际上都极大地阻碍着反复辟大革命运动的顺利开展，它们的存在只能使中国无产阶级反复辟大革命运动被无情地拖延。如果能将这些不利因素转为有利因素，整个中国左派队伍心往一处想，劲往一处使，三年之内搬到这个统治集团又有何难？厨师刀下拍黄瓜――小菜一碟嘛。所以，所有号称毛泽东主义者的左派同志和我党党员，都有责任从政治思想上、革命理论上、实际行动上高标准要求自己，自觉扫除我们自己的一切错误观点和障碍！只有这样，才能为反复辟大革命开辟道路！只有这样，才能迈开人民推动历史前进的有力步伐！ <br />
<br />
九、中国共产党修正主义叛徒统治集团非下台不可的声明 <br />
<br />
毛主席指出：“马克思主义这个真理是不可抗拒的，人民群众总是要革命的。”随着反复辟大革命形势的发展，修正主义统治集团内部矛盾也会快速的发生微妙的变化，这种变化会不断的被催化而发生作用。革命的风会吹进军营，造反的意识会在军队掀起波澜。每一个军人都会思考自己的立场――倾向革命的军人是大多数，倾向反革命的只是可怜的一小部分。军队的分化会成为必然。负隅顽抗的修正主义头子和反动军官会逐渐的被孤立起来。统治集团头子最后的选择无非是――要么是在全国罢工、罢课、罢市中被“罢免”而下台，要么是在无产阶级的文攻武卫中被人民革命的力量推翻而下台，要么是在人民革命舆论压力下被动谈判而下台。无论是哪一种下台方式，总之是非下台不可。这是毫无疑义的！ <br />
我党在这反复辟大革命的第一时间里就奉劝了反革命统治集团头子，现在还是那句老话，“识时务者为俊杰”，早下台早主动，晚下台就被动，越晚下台就越被动。如果通过谈判而下台，则是最明智的选择，可谓是早交权早解脱，早交权早安生。这样不仅可以为自己留下一条极尽体面的生路，而且也为自己的后代、家族留下一条奔向光明的出路。如果统治集团王八吃秤砣，死不改悔而大开杀戒，这样的话，罪孽就大了，自己不仅会留下千古骂名而且会给自己的家族带来毁灭性的灾难。至于无产阶级，无非是经历一次新的血与火的革命历炼和战斗洗礼而已。 <br />
<br />
十、毛泽东主义共产党基本主张的声明 <br />
<br />
&nbsp; &nbsp; 1、把大大小小的走资派、官僚腐败分子统统赶下台去，废除修正主义叛徒统治集团强加于中国人民的资产阶级宪法、法律。建立人民监督委员会，人人参与国家、军队、政府的管理和监督，保证人民是国家的真正主人。 <br />
&nbsp; &nbsp; 2、凡是1977年以前的公有制单位一律全部恢复。所有原单位失业下岗人员无条件恢复待遇。被侵占的所有公有制单位财产一律进行追查和清算。 <br />
&nbsp; &nbsp; 3、凡是1977年以后建立的垄断性所谓国有制股份企业一律交由人民接管。 <br />
&nbsp; &nbsp; 4、农村恢复人民公社。具体到每一个村庄，我们尊重当地群众的选择，愿意走集体道路的国家予以支持，愿意继续维持原样的可以保留。 <br />
&nbsp; &nbsp; 5、废除军衔制，整顿军队，恢复军队的革命化、现代化建设。时刻准备痛击任何敢于冒犯我国的外国强盗的武力威胁和军事挑衅。 <br />
&nbsp; &nbsp; 6、所有小学、中学、大学除书本费外，一切名堂的费用一律全免。大学实行免费用餐、免费住宿，除此以外给予适当的学业补助。培养又红又专的社会主义新人。保证大学毕业生全部安排工作。 <br />
&nbsp; &nbsp; 7、全国县、市建立新的现代化人民医院、中医院，彻底改变服务质量。无论城乡，只要是中华人民共和国的公民就医看病，除医药用品成本费以外一律全免（医药用品成本费由国家统一定价、按需配给）。执行毛主席“把医疗卫生工作的重点放到农村去”的指示，公社建设新的卫生院，村村建立卫生诊所。保证人人可以方便看病，看得起病。彻底改变穷苦人民小病拖延、大病等死的悲惨状况。 <br />
&nbsp; &nbsp; 8、凡是丧失劳动能力的老人，无论城乡一律享受退休金待遇。对于没有劳动能力残疾人，一律由国家妥善安置，彻底免除老人、残疾人的养护之忧。 <br />
&nbsp; &nbsp; 9、凡是中华人民共和国成年公民的工作，一律由个人申请自愿，国家统筹安排为原则进行安置。实行各尽所能、按劳取酬的社会主义分配原则。 <br />
&nbsp; &nbsp; 10、只要不是卖国投机、巧取豪夺、害民坑人的私人企业和公司，一律可以正常经营。同时，保护并帮助每一个自愿从事个体经营的商户正当经营。 <br />
&nbsp; &nbsp; 11、凡是中华人民共和国的成年公民，保证每人都可以拥有属于自己的一处住房。 <br />
&nbsp; &nbsp; 12、入党、提干遵照中国毛泽东主义共产党章程，可以坐飞机，可以坐火箭。 <br />
&nbsp; &nbsp; 13、凡是在反革命大复辟中变节，为“改革开放”鸣锣开道；与统治集团沆瀣一气的人，永远不准其担任国家部门的任何领导职务。对在反复辟大革命中能够同统治集团划清界限、反戈一击的人则一律既往不咎。 <br />
&nbsp; &nbsp; 14、对于凡是在反复辟大革命运动中表面积极投身革命，实际上又千方百计谋求个人私利的投机革命者，没有资格担任党和国家机关的任何领导工作。 <br />
&nbsp; &nbsp; 15、革命干部是人民的勤务员。党和国家的主要领导干部除必要的警卫员和助理员以外，一律不准配备个人秘书、医生、厨师、司机等服务人员。 <br />
&nbsp; &nbsp; 16、所有党的领导干部的工资，不得超过全国工人的最高工资。住房不得超过150平方米。 <br />
&nbsp; &nbsp; 17、凡是行贿受贿、危害人民群众利益，不看群众脸色办事的干部一律免职。 <br />
&nbsp; &nbsp; 18、凡是革命烈士、革命者的家属、后代及见义勇为的英雄一律予以必要的、全面的特殊照顾。 <br />
&nbsp; &nbsp; 19、凡是携款外逃的贪官，无论他逃到那里都要坚决追捕和通缉归案！对于拒不归案的贪官，无论他躲藏在哪里，都要不惜一切代价就地处决。 <br />
&nbsp; &nbsp; 20、凡是对抗第二次社会主义革命、对抗无产阶级专政的反革命右派分子，一律监督改造，对于态度恶劣的反革命分子坚决实行镇压！ <br />
我们的主张远不止这些，不远的将来，人民将为信仰和荣誉而工作，人们的思想和生活方式会发生质的变化，资产阶级庸俗腐朽的思想和生活方式将被彻底抛弃，一个崭新的社会主义新社会将成为人类历史发展的榜样。一个崭新的社会主义现代化强国将巍然屹立于世界的东方而成为真正意义上的――东方明珠。</div>

							
							
							
							
