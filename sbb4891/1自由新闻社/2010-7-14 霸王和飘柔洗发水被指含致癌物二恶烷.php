																											<div id="postmessage_739321" class="t_msgfont">其，不止洗l水、认词用的|西基本都含有<br />
今年3月，V州日蟮蟮谰鸵呀指出@c了。而@次霸王暴露，s是因橄愀塾C做的化，不止楹为化霸王的a品？<br />
<br />
国内外知名洗发水被检出二恶烷<br />
点击次数：141 发布时间：2010-3-19 V州日<br />
 <br />
你知道你用的洗发水是否含有致癌物质?洗发水是消费者每周都要使用的日化产品，但很少有人去关注其成分和含量。然而，记者在近期展开的调查中发现，市面上大多数洗发水产品均含有一种叫“二恶烷”的致癌物质，而且个别含量不低。<br />
<br />
　　在广州某大型超市，记者购买了市面部分畅销的10款洗发水产品，其中包括部分国内外知名洗发水品牌。随后，记者将这些洗发水送至“广东省保化检测中心”，（该机构是省级卫生化妆品监督检验机构，属于国家卫生部全国化妆品卫生监督抽检指定检验机构、广东省食品药品监督管理局认定的法定监督检验机构）和“广州市质量监督监测研究院”（该机构是三大国家级化妆品质量监督检验中心之一），两家权威机构进行检测。<br />
<br />
　　几天之后，记者拿到了两大中心提供的检验报告，两份报告一致检测出：这10款畅销的洗发水，其中9款均被检测出含有二恶烷。<br />
<br />
　　二恶烷有多毒?<br />
<br />
　　据华南理工大学化工学院钟振声副教授介绍，二恶烷虽然是毒性物质,但是要看它对人体造成的危害主要看量有多大。如果二恶烷以溶液等形式被人喝进肚里，就会直接进入血液，危害比较大。但是如果以膏状等涂抹于皮肤之上，经过皮肤吸收之后再进入血液中的量也许就很少了。但具体有多大量会产生危害，还要进行毒理测试才能确定。<br />
<br />
　　二恶烷含量最高达19.5<br />
<br />
　　美国标准最多为5<br />
<br />
　　根据“广州市质量监督监测研究院”出具的检验报告，这9款洗发水的二恶烷含量最高为19.5。最低也达到了5.9，含有二恶烷的9款洗发水中，有7款产品的二恶烷含量超过了10。由于中国没有出台二恶烷含量的限量标准，而全球知名化学品公司禾大国际相关人士对记者说，美国的化妆品原料（表面活性剂，也称AES）的二恶烷行业标准为20，“表面活性剂”在洗发水中的添加量一般为15~20%，因此，洗发水成品中的二恶烷含量正常为3~4，加上还要添加其他活性剂，洗发水中二恶烷含量最多也应该是5。但根据广州日报委托的检验结果看，多数洗发水这一含量在10~20之间。<br />
<br />
　　内行爆料：洗手液沐浴露也含二恶烷<br />
<br />
　　一位消费者在他的博客中说：这让我们知道二恶英之外，又有一种叫二恶烷的致癌物质。想想咱们中国人也真经得起折腾，总有一天，咱们会锻炼得百毒不侵的。<br />
<br />
　　还有消费者说，同样是洗发水，为何有的含有这种有害物质，有的却不含呢?这说明还是跟企业的社会责任心有关，企业不能见利忘义，政府部门也不能忽视监管，不然，这种有害物质只会越来越泛滥。<br />
<br />
　　“不光是洗发水，其他日化产品也或多或少含有二恶烷!”广东省保化检验中心主任郑伟东说，根据我们检验的结果，市面上的洗发水大多数含有二恶烷，洗手液中一部分产品含有二恶烷，沐浴露中较少一部分产品含有二恶烷。这些产品中，二恶烷含量有轻有重，主要取决于原料的质量。<br />
<br />
　　看到报道后，一些专业人士还打来电话，建议本报提醒消费者注意：尽量少使用免费供应的沐浴产品。广东一位原料供应商人士来电说：桑拿和洗浴场所的免费洗浴产品中的有害成分更多，二恶烷含量更高。<br />
<br />
　　消费者号召多用天然物质<br />
<br />
　　还有消费者建议“少用化学品，多用天然物质”。他认为，化学洗涤剂实际上就是石油副产品。在这些被包装得多彩多姿的化学洗涤剂的使用过程中，人们正在不知不觉地如同吸毒般地依赖着它。<br />
<br />
　　“还是用回以前的那些天然洗发物质啦!”不少消费者发出号召，比如用淘米水、茶籽粉、捞饭汤、云南皂角、芦荟汁、蛋清等来洗发，既便宜又放心。一位消费者还详细介绍了用蛋清洗发的方法：首先用水将头发洗干净，然后将蛋清打入手心，将手心的蛋清在头发上来回揉搓，约5分钟左右，将头发漂洗干净就可以了。<br />
<br />
　　不含二恶烷原料价贵5倍<br />
<br />
　　洗发水企业能否在技术上将这种有害物质避免带入到化妆品里面去呢?香港化妆品化学师学会陈创光的回答是肯定的。<br />
<br />
　　洗发水企业为何不做这项工作呢?几位专业人士均表示：一是意识问题，二是成本问题。<br />
<br />
　　陈创光表示，含二恶烷的表面活性剂（AES）并不是不可代替，目前已经有新的表面活性剂能达到更好的效果，不过价钱要贵4到5倍，因此很多企业不愿意采购这种替代原料。<br />
<br />
　　企业：不会掏钱去做检验<br />
<br />
　　国家化妆品监督检验中心相关人士在记者送检的时候告诉记者，他们只负责检验二恶烷的含量，但是合格与否他们不做判定。<br />
<br />
　　国家食品药品监督管理局去年曾表示，化妆品中含微量二恶烷不会对人体产生伤害，但是到底多少才是“微量”，目前也没有一个标准。<br />
<br />
　　美国对洗发水原料有行业标准为20PPM，成品的二恶烷含量应在10PPM以内。<br />
<br />
　　而一些被检出二恶烷含量的企业昨日向本报记者表示，国家并没有要求他们对采购的原料和成品中的二恶烷进行检测，也没告诉他们含量多少是“红线”，他们当然不会掏钱去做检验。</div>

							
							
							
							
