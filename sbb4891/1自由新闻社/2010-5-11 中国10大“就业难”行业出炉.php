中国1份民间调查报告首度披露大学生10大「就业困难」科系，电脑资讯科技、国际贸易等学科赫然在列，显示两岸大学生都面临相似的困境。<br />
<br />
「成都商报」报导，上述讯息来自麦可思公司最新公布的大学生就业报告。这份报告调查2006到2009年中国大学生的就业概况，样本数达到100多万份，是目前中国大陆最权威的民间就业调查数据。<br />
<br />
据报导，调查人员以「红牌」来预警，所谓「红牌专业」是指失业量较大、就业率持续走低，且薪资较低的专业中综合考虑的前10个专业，为高失业风险型专业。<br />
<br />
就本科（大学）专业而言，包括动画、法学、生物技术、生物科学与工程、数学与应用数学、体育教育、生物工程、计算机（电脑）科学与技术、英语、国际经济与贸易等专业都被列入其中。<br />
<br />
而专科中，「红牌专业」包括：临床医学、法律文秘、计算机（电脑）科学与技术、国际金融、工商管理、经济管理法律事务、汉语言文学教育、计算机（电脑）应用技术、电子商务等。<br />
<br />
另外，调查人员还列出一些失业量较大，就业率持续走低，且薪资较低的专业，被称为「黄牌专业」。本科中包括美术学、艺术设计、统计学、电子信息（资讯）科学与技术、公共事业管理、信息（资讯）管理与信息系统、工商管理、汉语言文学等。这些专业虽然就业情况不如「红牌专业」那么严重，但仍不能大意。<br />
<br />
报告同时列出一些就业情况好转的专业，包括地质工程、港口航道与海岸工程、船舶与海洋工程、石油工程、采矿工程、油气储运工程、矿物加工工程、过程装备与控制工程、水文与水资源工程。</div>

							
							
							
							
