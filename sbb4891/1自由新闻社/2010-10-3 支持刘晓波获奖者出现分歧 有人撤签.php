<strong><font size="6"><font color="Black">支持刘晓波获奖者出现分歧 有人撤签</font></font></strong><br />
<br />
<font size="3"><font color="SlateGray">记者: 雨舟 | 华盛顿</font></font><br />
<br />
　　今年的诺贝尔和平奖将于本月8号揭晓获奖名单，12月10号颁奖。在海内外人士呼吁把奖项授予《零八宪章》起草人刘晓波的同时，刘晓波的支持者中也出现“异议”之声。<br />
<br />
　　<strong>*江天勇撤回签名 反对厚此薄彼*</strong><br />
<br />
　　百名中国知识分子和维权人士签名发出公开信要求诺贝尔和平奖委员会把今年的诺贝尔和平奖颁发给正在监狱服刑的中国异议人士刘晓波之后，北京的律师江天勇日前发表声明，宣布撤回自己的签名。<br />
<br />
　　江天勇在声明中说，他“经过认真观察和思考”，决定“撤回呼吁书上的签名”；这个决定虽然“痛苦和艰难”，但是，这是“对自己良心负责的决定”。<br />
<br />
　　声明显示，江天勇并非改变对刘晓波的支持立场，而是出于对部分呼吁者在支持刘晓波的同时，有“贬损”维权活动人士高智晟、胡佳和陈光诚之嫌的做法持有不同意见。<br />
<br />
　　<img src="http://c.imagehost.org/0146/2_8.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　<font color="Blue">北京的律师江天勇</font><br />
<br />
　　美国之音记者通过电话采访江天勇时，他表示，对这一举动不方便做进一步说明和评论：“可能这是我唯一一次拒绝记者采访， 但是，这个我不方便讲，不好意思，真的不方便讲。”<br />
<br />
　　<strong>*浦志强：挺刘和维权不矛盾*</strong><br />
<br />
　　同样位于北京的律师浦志强对美国之音表示，他了解江天勇律师做出决定的来龙去脉，因为作为呼吁书的签名者之一，自己就是对高智晟、胡佳和陈光诚等维权人士发表过不同看法的人。<br />
<br />
　　浦志强说，他赞赏高智盛等维权人士的抗争精神，但是对他们抗争方式的确存在不同的看法。不过，他感觉支持刘晓波与对维权方式的看法并不互相矛盾。<br />
<br />
　　<img src="http://c.imagehost.org/0917/3_1.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　<font color="Blue">北京的律师浦志强</font><br />
<br />
　　浦志强说：“我个人感觉两者之间没有矛盾。我在推特上发表的言论，是这几天香港、法国、英国、德国有些媒体，包括电台、通讯社和报纸，在采访这个话题时问到这几个人里面你认为谁更合适。所以只是一种意见的表达。”<br />
<br />
　　<strong>*避免无畏牺牲*</strong><br />
<br />
　　浦志强表示，他认为可以选择理智的方式来与执法部门相处，没有必要做出无畏的牺牲。他说：“应该认识到，警方乱来是他们现在的存在方式和表达方式。我们注定要和乱来的国保、和逐渐文明起来的公安相处若干年。我自己愿意采取的方式是，想清楚自己的原则，尽量不要跟个人产生肢体上的冲突。”<br />
<br />
　　浦志强表示，希望有机会与包括江天勇律师在内的意见分歧者进行交流和沟通，以便增进互相了解甚至化解分歧。&nbsp; &nbsp;<br />
<br />
　　<strong>*北京批刘但否认施压*</strong><br />
<br />
　　在支持刘晓波获奖的呼声高涨的同时，中国政府发出了警告。外交部发言人姜瑜日前在新闻发布会上表示，刘晓波坐牢是“因为违反了中国法律”。她说，诺贝尔奖是奖励那些推动人类和平、推动国际友好关系和为裁军作贡献的人，“刘晓波的行为与诺贝尔和平奖背道而驰”。<br />
<br />
　　不过，中国政府否认施加压力。外交部发言人姜瑜说，在人权问题上出现分歧“是正常的”，中国“无意向别国施加压力”。<br />
<br />
　　<strong>*诺委会曾被施压*</strong><br />
<br />
　　挪威的诺贝尔和平奖委员会说，它受到过来自中国方面的直接压力。该委员会秘书伦德斯塔对美国之音表示，中国外交部副部长傅莹今年6月曾经对他说，中国政府反对中国异议人士获得诺贝尔和平奖，否则将被北京“视为不友好举动”，将为两国关系带来“不和谐因素”。<br />
<br />
　　不过，伦德斯塔说，中国并非首次发出类似警告，这对诺贝尔委员会的工作不构成影响。他对美国之音表示，诺贝尔委员会“是个独立的委员会”，完全独立于挪威政府之外，所以“不会照任何人的话办事”，包括挪威政府。<br />
<br />
　　今年54岁的刘晓波是倡导中国政治改革《零八宪章》的起草人之一，去年底被北京法院以“煽动颠覆国家政权罪”判处11年有期徒刑，目前在辽宁监狱服刑。</div>

							
							
							
							
