																											<div id="postmessage_40663" class="t_msgfont">隐含在兽首中的皇家意识 <br />
<br />
沈嘉禄 <br />
<br />
拍卖二字，是近日新闻报道的关键词之一。事起法国佳士得拍卖行准备于2月23日至25日举办一个专场拍卖会，其中让中国人目瞪口呆的是从圆明园流失的两个兽首铜像――鼠首、兔首。这两件拍品估价相当于人民币2个亿。老外开出的这个价位，明摆着给虚火上升的中国人吃药。他们相信，在爱国主义和民族主义的煽动下，中国人心气一定比大西洋还辽阔。 <br />
<br />
圆明园兽首是中国人心头的痛。这里不妨简单回顾一下，2000年，佳士得和苏富比在香港举行的春拍上，圆明园海晏堂大水法的牛首、猴首和虎首铜像首次现身。中国保利集团闻风而动，以超过事先想象的价位咬紧牙关拍下。 <br />
<br />
2003年初，中华抢救流失海外文物专项基金在美国寻访到猪首铜像的下落。经过努力争取，美国收藏家同意将猪首转让给该专项基金。2003年9月爱国富豪何鸿隹犊出手，向这家基金捐了人民币600余万元后将猪首“赎回”。 <br />
<br />
2007年8月初，尝到甜头的香港苏富比干脆以“八国联军―圆明园遗物”为名开设专场，大水法“十二太保”中的马首被何鸿鲆荤智；兀捐给国家。 <br />
<br />
圆明园“十二太保”中五个同门兄弟相继回归故园，使得海外华人组织寻访“十二太保”的热情越来越高涨，大有踏破铁鞋也要找齐的决心，这不由得让人联想起“ 孤星血泪”之类的情景模式。果然，老天不负有心人，2003年到2004年间，中华抢救流失海外文物专项基金终于探得了鼠首和兔首的下落，但因为收藏者报价时 “狮子大开口”，此事就没法谈了。 <br />
<br />
现在，这两个兽头露面了，再一次激发了中国人民的强烈反响。反响声中震耳欲聋的是两字：“追索”。理由是，这十二件兽首铜像是在第二次鸦片战争中被英法联军掳去的，理应还给中国。 <br />
<br />
与民间呼声相对应的是，昨天，中国外交部发言人姜瑜在例行记者会上重申对这两件流失文物的所有权。她强调：拍卖战争中非法掠夺的文物不仅伤害中国人民的感情，损害中国人民的文化权益，而且有悖有关国际公约，希望有关方面能够慎重予以考虑。 <br />
<br />
但是，外交部这个态表得稍晚了一拍。据中新社10日的报道：法国佳士得拍卖行决定停止拍卖这两件兽首。 <br />
<br />
境外媒体的报道认为：法国人之所以作出这个决定，是因为香港爱新觉罗家族宗亲会愿意成为圆明园兽首原告，并请了一个由81名律师组成的律师团，对他们施加了压力，迫使法国佳士得与委托人达成协议，转为私下交易。 <br />
<br />
这一点应该引起我们的警惕。 <br />
<br />
私下交易，如果没有头脑发热的中国人轧一脚的话，我有理由预计，原估价的五分之一也不会到。反之，如果这两件东西由中国人参与竞拍的话，很可能创下拍卖新纪录，当然也是新的世界级笑柄。设想一下吧，外国人一件一件吊足中国人的胃口，骗走中国人的巨款，直至“十二太保”中最后一件隆重登场，开出的价位肯定是天价中天价啦。这种文物交易的游戏，地球人都知道。但是本文的焦点不在于此，我想提醒大家的是，无论从保利集团从回购第一件兽首开始还是到今天的一鼠一兔的出现，中国民众表现出来的爱国热忱当然应该肯定，但也不能排除，其中的皇家意识起了一定的加温作用。 <br />
<br />
比如香港爱新觉罗家族宗亲会，里面汇集了爱新觉罗家族成员，平时搞搞清史研究，甚至弄出点宫闱野史来也不是不可以。但我们必须明白，向外国政府追索流失文物，只能是现任政府而非一个被人民抛弃、由非法政府后裔组成的民间团体。“爱新觉罗”四个字之所以在香港有号召力，以致让素来理性的律师激动一把，不能不说是皇家意识在作怪。如果是法国人存心要买“爱新觉罗”的面子，那就是一个政治问题了。在主权问题上，我们不能丝毫含糊。 <br />
<br />
再比如，熟悉中国艺术史的人都知道，圆明园的建筑与成千上万“玩意儿”，其中有相当一部分在工艺上代表了当时的先进水平，但艺术品格并不高。比如那一套兽首，合金铜浇铸，写实风格，从美学上来评估，那是相当差劲，毫无美感可言。即使从浇铸工艺上说，与太和殿前的一对铜狮子也不可同日而语。牛首马首一经拍卖后，香港的古玩市场上就出现了非常逼真的仿品，香港人买回去钉在卫生间里挂睡衣。上海方浜中路藏宝楼的地摊上也露过两眼，包浆做得很逼真，我询过价，2000元一件还可以还价。 <br />
<br />
在娱乐化的时代，清宫秘闻是一个可供影视人不断发掘的矿藏，也赚足了民众的眼泪和钞票。在此大背景下，清代宫廷艺术品往往能拍出天价，折射一部分国民的艺术修养还很欠缺。相比之下，民间艺术虽然在历史文化信息的承载和美学影响上高出“官窑”好几倍，但经济价值都没有达到应有水准。比如洛阳龙门石窟的石佛首、手、足，艺术价值不知高出圆明园铜兽首几十倍，被盗而流失国外的有几十件，但政府与民间的追索热情与力度不能与兽首相比。所以，落后的皇家意识不仅影响了对艺术品和文物的价值评判，甚至会影响我们对意识形态和政治制度设计的把握。</div>

							
							
							
							
