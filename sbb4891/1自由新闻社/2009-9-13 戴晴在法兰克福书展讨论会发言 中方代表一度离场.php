													<h2>戴晴在法兰克福书展讨论会发言 中方代表一度离场</h2>
						
						
																											<div id="postmessage_238265" class="t_msgfont">更新：<strong>2009.09.26 法兰克福书展前：中国拒绝作家廖亦武出境 </strong> https://1984bbs.com/viewthread.php?tid=22146&amp;page=2#pid258516<br />
<br />
~~~~~~~~~~~~~~~<br />
<br />
德中/欧中 | 2009.09.12&nbsp;&nbsp;Deutsche Welle<br />
<br />
本周六（9月12日）在法兰克福书展框架之内召开的一项中国问题讨论会上发生轰动性事件。当“中国与世界--感受与现实”讨论会的德方组织者临时改变会议议程，邀请对中国政府持批评态度的作家戴晴上台发言时，中国官方代表团中的大部分成员离开会场，其中包括中国驻德大使馆参赞赵彬以及前驻德大使梅兆荣，现场一度陷入混乱，局面尴尬。 <br />
<br />
尽管会议主持人强调，本次讨论会的目的就是让不同的意见得到交流的机会，但中方的大部分人员依然退场，在戴晴和贝岭发言完毕之后才再度返回。戴晴在此后的发言中对中方代表退场的行为表示非常遗憾。而法兰克福书展主席博斯则对此表示非常失望。德国笔会秘书长维斯内尔表示，中方这样的行为“不是一个好的信号”。<br />
<br />
法兰克福书展方面此前邀请戴晴和贝岭两人出席讨论会，但随后又撤回了对两人的邀请，舆论普遍认为，戴晴和贝岭“出局”是因为法兰克福书展方面受到了讨论会的中方合作者中国国家新闻出版署的压力。此事引起德国舆论的高度关注，周六的讨论会开幕式吸引了大量媒体记者。基民盟籍的法兰克福市市长罗特在讨论会开幕式上批评书展组织方“不够聪明”的做法，并表示为民主代言的人应该有立场，已经发出的邀请不应该因为受到压力而撤回。中国驻德大使馆参赞赵彬在随后的发言中表示，中国和西方国家在文化传统发展道路上不同，双方有分歧，因此应该相互尊重。<br />
<br />
在戴晴和贝岭先后发言之后，中方代表返回场内。曾任中国驻德大使的梅兆荣在此之后表示，讨论会组织者在事先没有通知中方的情况下邀请戴晴和贝岭发表谈话，中方退场是对此表示抗议。另外，梅兆荣还对法兰克福市长罗特女士的发言作出了回应，他表示，罗特女士在致词的时候一再突出戴晴和贝岭与会的重要性，但对其他的中方代表几乎视而不见。梅兆荣称，中方与讨论会组织者进行交涉之后，组织者方面已经正式作出道歉，中方才决定重回会场。据本台记者现场观察，此后讨论会在“相对较为正常”的气氛下继续进行。<br />
<br />
在随后的讨论会进程中，戴晴向梅兆荣提出问题，但并未得到正面回答。而梅兆荣则表示，戴晴和贝岭并不代表大多数的中国人，让他们参与这样的会议令人感到遗憾。本台记者针对这一说法向梅兆荣提问，如何确定谁能够代表大多数人，那些不属于“大多数中国人”的个人是否有权利和资格表达自己的意见。但梅兆荣本人也未就记者的提问做出全面回答，而是由中国社科院的一名代表回答称，中国共产党是代表中国大多数人利益的，是通过法律确定下来的，而中国共产党的建设成就也代表了大多数人的利益。<br />
<br />
本年度法兰克福书展将于10月14日至18日举行，中国是今年书展的主宾国。<br />
