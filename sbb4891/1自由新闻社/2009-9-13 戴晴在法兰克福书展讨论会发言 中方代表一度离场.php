													<h2>异议作家邀请被拒事件给法兰克福书展蒙上阴影</h2>
						
						
																											<div id="postmessage_238273" class="t_msgfont">德中/欧中 | 2009.09.10 <br />
<br />
法兰克福国际书展不仅是全世界最重要的图书、媒体产品、版权交易市场，也是一个给思想创意和自由讨论提供广阔空间的平台。然而今年的书展主宾国却让自由公开的讨论变得有些艰难。今年的主宾国是中国。北京当局显然已经开始阻止一些异议作家在法兰克福书展上露面。原本计划参加本周末在法兰克福举行的、同今年书展相关的一次国际研讨会的中国异议人士戴晴和流亡作家贝岭就被德方正式邀请&quot;出局&quot;。&nbsp;&nbsp;<br />
<br />
9月12和13日两天，在德国法兰克福将举行一次由来自中国和其他国家的著名作家、专家和学者的国际研讨会。研讨会的主题是&quot;中国与世界-感受与现实&quot;。会议的主旨理论上是让与会者交流观点，增进理解，消除误解和成见。但实际情况又是怎样呢？本届法兰克福国际书展的主宾国中国看起来对自由公开的意见交流并不感兴趣，因为北京方面不希望异议人士戴晴和流亡作家贝岭出席这次国际研讨会。戴晴是中国调查型记者之一，也是积极的环保人士。对于中国新闻出版署拒绝转交给她德国发出的邀请函，戴晴表示极为气愤：<br />
<br />
&quot;我是国民，我是中国作家，我又是一直在中国生活写作的。你开这个国际研讨会，目的就是让中国作家谈谈中国文学状况。你到底说出理由来，我怎么就不能去说出我的感受呢？&quot;<br />
<br />
彼得・利浦肯是德方这次国际研讨会的组织者。两年来利浦肯一直参与法兰克福国际书展的工作。在接受德国之声采访时，利浦肯介绍道，中方威胁说，如果戴晴不退出出席这次国际研讨会，就将集体取消出席计划：<br />
<br />
&quot;众所周知的是，戴晴同北京政府间存在一种矛盾。所以中方完全不希望她参加这个研讨会。&quot;<br />
<br />
利浦肯强调研讨会的独立性，这不是主宾国中国举办的活动，而是书展的合作伙伴，其中也包括作家组织独立笔会，一起倡议举行的研讨活动。但是利浦肯也表示，很希望能有中国官方的代表能够出席：<br />
<br />
&quot;我们希望搞一个双方都能发表观点意见的研讨会。我们不能只同来自欧洲的学者或汉学家进行交流。所以我们也邀请了中国官方的一些人士与会。&quot;<br />
<br />
作为今年法兰克福国际书展的主宾国，中国总共投资了500万欧元。届时将有数十位作家在书展上露面。数月以来，已经有众多相关的活动启动，让观众对即将上演的&quot;中国秀&quot;有个准备。至于中国这些努力的目的，波恩汉学家司马涛这样分析说：<br />
<br />
&quot;中国希望打造一个光辉形象。它不愿意以一个充满问题和冲突的国家展示在世人面前。中国多年来试图建立和谐社会，这只是个理想目标。实际上许多矛盾和问题都只是被掩盖了起来。&quot;<br />
<br />
而现在这些问题却显露了出来。中国不希望在周末的法兰克福国际研讨会上同异议人士戴晴坐在一起进行探讨。同样被邀请&quot;出局&quot;的流亡作家贝岭在接受德国之声采访时表示：<br />
<br />
&quot;独立的表达，一个作家或学者的言论自由，这是一个原则问题。不应该有任何的改变，因为这是书展的传统。如果说书展今年的活动里面连这个部分都不能坚守的话，我认为这是对法兰克福书展，对书，对出版，对自由和表达的，对社会的严重的伤害。如果是因为中国的参与，而使这个伤害在德国成为现实，这简直是不可想象的事情！&quot;<br />
<br />
9月10日，法兰克福书展组织方将召开一个新闻发布会。有关中方施压导致一些作家不得出席书展相关活动的问题也会成为发布会上记者关心的话题。根据德新社9月10日11点最新消息，虽然法兰克福国际书展的主办方不再安排戴晴出席书展讨论会，但戴晴仍将受德国笔会的邀请于本周五赴德，并出席周末的国际研讨会。<br />
