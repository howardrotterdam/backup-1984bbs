																											<div id="postmessage_811393" class="t_msgfont">台学者反驳中共领导抗战胜利之说<br />
2010-09-03 02:04:40<br />
<br />
【多维新闻】抗战胜利纪念日大陆官方媒体宣称抗战由中共领导，台湾历史学者对此予以反驳，认为抗战是在国民党政府的统一领导下进行的，国军组织了正面战场的大规模战役，中共说法不符合历史事实。<br />
 <br />
据中央社9月3日报道，《人民日报》指抗战胜利是因中共领导，国民党党史馆主任邵铭煌以历史学者身份反驳，这是不符合客观事实的陈腔滥调，中共当年发表《共赴国难宣言》可证明一切。<br />
 <br />
3日是中国官方所定的抗日战争胜利65周年纪念日，《人民日报》发表社论说，中国战胜日本的最根本原因，是中共代表全中国人民的意志，领导和推动抗战。<br />
 <br />
对此，邵铭煌上午强调，他以历史学者角度来看，这是陈腔滥调的说法。他指出，1937年7月22日中共发表《共赴国难宣言》可以证明一切。<br />
 <br />
他说，中共发表《共赴国难宣言》，表示愿意服从三民主义，为实现三民主义奋斗，同时也表示愿意取消推翻国民党的暴动政策、取消苏维埃政府，改编红军为国民革命军，接受国民政府军事委员会管辖。<br />
 <br />
邵铭煌表示，共军之后纳入国军系统，接受国民党的领导，抗日成功。因此，人民日报所谓的“中共领导全民抗战”，并非事实。他说，共军有参与，也有动员，但不是他们所指挥，而是中华民国政府的领导；当时就是国民党执政，动员全国的人力物力投入抗战，共军是呼应国民党号召才共同抗日。<br />
 <br />
他说，大家都知道，8年抗战是国民党、共产党共同合作抗日。在那个时代，是国民党主政，大家共同对付日本帝国主义，举全国之力共同抗日。<br />
 <br />
邵铭煌指出，抗日大大小小战役加起来共有200多次，大的战役都是国军奋勇抵抗，包括上海保卫战、南京卫战、武汉保卫战及重庆大后方等，全都是中华民国政府的力量。<br />
 <br />
因此，邵铭煌说，所谓中共领导全民抗战，并非事实，这是不符合客观事实的论调。<br />
 <br />
在中共官方抗战史中，当年国民政府采用了片面抗战路线，未能发动群众，因而导致在正面战场屡屡导致败北，淞沪会战、枣宜会战、太原会战、豫湘桂会战之惨败使得中国丧失华北、华东、华中、华南大半江山，而中共领导的抗日武装却以名义上两个师的兵力在敌占区牵制大量日军，充当了抗战的主力。</div>

							
							
							
							
