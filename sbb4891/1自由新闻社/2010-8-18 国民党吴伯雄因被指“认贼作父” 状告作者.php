																											<div id="postmessage_787642" class="t_msgfont">我们讨论的是两个层面的问题，我之所以认为中共要为当前的局面负责是因为它作为执政党不行宪法、不施民主、垄断资源，假人民共和国之名，行法西斯独裁之实，因此要负责。<br />
<br />
你说的是民智未开、需要启蒙的问题，当然作为一个中国人的确也要为此负责。但是如果没有一个公平自由的对话平台，公民谈何“负责”？你也看到，一部分中国人已经受到启蒙的影响，愿意行使自己作为公民的权利并且履行一个公民的义务，但是一张口就跨省追捕，发个帖就成了“一小撮”，这又怎么解释？所谓天地有公道，公道在人心，可是人心里的公道一天到晚都憋在心里，屁都不敢乱放的公民，如何不成了“屁民”？再者说，自打清末以来，仁人志士为国分忧不可谓不多，但是“我爱大清，可谁爱咱？”没有言论自由，基本人权得不到保障，这是更广义上的解决“民智未开”问题的根本症结，而这又恰恰是执政当局所应当推动的。当然如果它今日不推动，明日不推动，早晚会有人来推动，免不了又是流血街头，此中三味，当事人自知。<br />
<br />
至于中国南北、东西文化差异的问题，的确存在。但事实摆在这里，无论是北方人也好，南方人也好，汉族人也好，少数民族也好，大家当下是真真切切同在中共的屋檐下，不解决专制问题，任何文化冲突都无法解决。而至于是否应当实行分治，是应当在一个民主公正的对话机制上通过理性的方式来解决。而中共执政的当下中国，挟北方文化压制、丑化南方文化的现象层出不穷，殊不知南方人维护南方本地文化也是基本人权之一么？人权不保，谈何文化？</div>

							
							
							
							
