袁崇焕热血名句　遭官方下令铲走<br />
<br />
<img src="https://yopic.us/images/14ch1pa.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
明朝将领袁崇焕雕像下口头禅「掉哪妈！顶硬上！」被铲去，令广东话I卫战越演越烈。互联网<br />
<br />
率先由广州燃起的广东话I卫战越演越烈。「掉哪妈！顶硬上！」这句据称是明代名将袁崇焕打仗时的口头禅，当局亦容不下！袁在东莞石碣故乡纪念园中的雕像基座上，原刻有一句袁带兵打仗时脱口而出，具鲜明岭南文化特色、生动反映一代名将热血性格的方言，近日竟遭东莞文化部门下令铲除。事件引起舆论和民众强烈不满，令广东话I卫战火上加油。<br />
<br />
石碣袁崇焕纪念园是当地村民多年前在袁的故居旧址上修建的，其中一座袁崇焕骑马跃前的雕像基座上，镶有一块金色铭牌，上面刻有一句据称是袁带兵打仗时的口头禅：「掉哪妈！顶硬上！」下配英文翻译「FUCKHISMOM！HITTHEHARD！」旁边还有一段中英文解释，意指「顶硬上」是袁当年保护京城的主旋律，而「掉哪妈」是骂昏君的助语词。<br />
<br />
当局指粗口不宜展示<br />
<br />
<img src="https://yopic.us/images/14ch2pa.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
原来的袁崇焕雕像，口头禅清晰可见。<br />
<br />
由于这句口头禅极具特色，深得游客欢迎，被视为是该景区最大亮点。有保安坦言，不少游客就是冲着这句口头禅而来的。但有内地记者近日发现，这块金色铭牌已被凿走，基座上只留下因凿而留下的痕迹。园内保安称，铭牌是近日凿去的，具体原因不清楚，「那是领导考虑的事」；有清洁工则称，牌子是领导拆的。<br />
<br />
本报记者昨致电袁崇焕纪念园查询，一名不愿透露姓名的男子称，自今年初该纪念园收归东莞市文化局管理后，领导就对铭牌有不同看法，认为是粗口，不宜展示，「领导仲话，又ぞ莼澳亓骄浠跋翟崇焕打仗时口头禅，所以要铲去」。他还说：「其实疃嗄甓尤私策，仲有国家领导人来睇过，亦无反对。唔知点解？家要铲去！」<br />
<br />
「掉哪妈！顶硬上！」被铲，引起民众不满。前日有游客在现场称，在网上看到纪念园有袁的口头禅，十分感兴趣，专门造访想合影留念，但可惜再见不到了，「现在只能拍这个残缺石像了」。据悉，该六字口头禅源自金庸论文《袁崇焕评传》，其中有描述：「全军上下在他（袁崇焕）激励下，人人热血沸腾，决心死战，『丢哪妈！顶硬上！几大就几大！』」<br />
<br />
