东莞石碣镇的袁崇焕纪念园外貌。<br />
<br />
<img src="https://yopic.us/images/14ch4pa.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
金庸曾经为袁崇焕故园题字。<br />
<br />
「所有讲广东话人的悲哀」<br />
<br />
近日，「掉哪妈！顶硬上！」遭网民狂转贴。有网民指，事件与近期广州掀起I卫广东话一样，是官场北方人要消灭广东方言，「简直是犯罪」，「这不仅是石碣人的悲哀，而是所有讲广东话人的悲哀」，「事实证明，I卫广东话的确逼在眉睫」。此外，广东省「I卫广东话之战」日炽，广州不少媒体纷纷加入争论。<br />
<br />
