																											<div id="postmessage_724647" class="t_msgfont">广州出现「捏捏族」 遭严批 <br />
 <br />
【中央社u台北6日电】 2010.07.06 05:30 pm <br />
 <br />
&nbsp;&nbsp;<br />
「心情烦躁、压力大的时候就跑到超市，捏捏方面面、饼干，甚至撕开食品袋子，感觉真刺激。」近来广州白领圈出现「捏捏族」，但这种减压方式引来道德批判。<br />
<br />
「捏捏族」多为一些年轻人，在下班后或周末到超市，以捏碎各种食物的方式来发泄情绪、缓解压力。<br />
<br />
中通社报导，广州各大超市偏僻货架，确实出现一些方便面（速食面）、饼干等物品被捏碎的迹象。此外，在牙膏专卖区，一些牙膏扭曲变形，很明显是人为挤压造成。<br />
<br />
广州某大型连锁超市工作人员说，「每天货架上的饼干总会受到不同程度的损害，尤其是傍晚时分，周末更甚。」但捏毕竟不同于偷，即使发现也只是制止。<br />
<br />
一位「捏捏族」说，每次去超市买东西都喜欢趁服务员不在的时候捏几包方便面，「那声音，那手感，让我得到极大的满足。」<br />
<br />
一位从事客服工作的广州白领人士表示，平常做的就是收集各类投诉，经常被客户骂得狗血淋头，完全成了出气筒。 「久了就觉得很压抑，最近到超市捏东西减压，做后确实蛮过瘾。」<br />
<br />
针对「捏捏族」现象，广州不少市民表示，如果成为「捏捏族」真的可以减压，那承当这种减压代价的就必然是商家。 「捏捏族」们得到放松和情绪的发泄，但这种行为却把焦虑推给了别人，要受到道德批判。<br />
<br />
心理专家表示，「捏捏族」的组成人群大多为城市年轻上班族，这种另类行为是都市白领的一种新型强迫症，是不健康的。白领应该有积极健康的减压方式，譬如参加体育运动，多与朋友沟通，参加心理咨询等。</div>

							
							
							
							
