													<h2>石家庄塌楼事件“一波三折” 媒体空前“变脸”</h2>
						
						
																											<div id="postmessage_190867" class="t_msgfont">中新网石家庄8月5日电 (记者 鲁达 陈国林)8月4日，河北省石家庄市的“新闻大战”随着大雨开始，又伴随着大雨暂时结束。 <br />
<br />
这一天，几乎所有的媒体都体验了“一波三折”、“一波未平，另一波又起”的味道。 <br />
<br />
中午12时许，官方媒体网站《河北新闻网》援引消防部门最先曝出“石家庄一旧楼大雨中倒塌”消息，此后，媒体新闻战在“阵脚混乱”中开始。在经历采访受阻事件后，事发时间、地点、原因不断“变脸”，连媒体值班编辑都有“眼花缭乱”之感，相信受众也在“雾里看花”。<br />
<br />
至于其中缘由，官方4日23时30分的通报认为，“倒塌房屋为砖混结构，临时避雨人员较多，无目击者确定被埋人数，给救援工作带来一定难度”。此外，当日雷电交加天气，不仅给救援和事故调查工作带来难度，也给媒体采访加大困难。<br />
<br />
事件发生后，河北省委书记张云川，省长胡春华，省委副书记、石家庄市委书记车俊高度关注，车俊明确要求“认真查明原因，及时向社会公布”。石家庄市长艾文礼第一时间赶到现场，迅速组织有关部门迅速开展人员救治、稳定和善后处理。应该说，此间官方特别是宣传部门，给予了记者力所能及的帮助和配合。<br />
<br />
