													<h2>石家庄房屋倒塌初步判定为雷击所致</h2>
						
						
																											<div id="postmessage_190757" class="t_msgfont">石家庄房屋倒塌初步判定为雷击所致<br />
2009年08月05日 00:03中国新闻网【大 中 小】 【打印】 15位网友发表评论<br />
<br />
中新网石家庄8月4日电 (记者 鲁达 牛琳) 4日23时许，此间官方公布石家庄塌楼事件最新调查结果。此前所称在建厂房被指为“在建民居”，并称经综合分析，初步判定闪电(球形)击中建筑物是建筑物倒塌的主要原因之一。<br />
<br />
据介绍，2009年8月4日上午，位于石家庄市西兆通镇南石家庄村的村民自建一临街房屋遭雷击突然倒塌。倒塌房屋为临街一排砖混结构村民自建二层房屋。主体已完工，尚未安装门窗，空房，房屋临街敞开。<br />
<br />
事件发生后，河北省委书记张云川、省长胡春华都做出迅速抢救被压人员的重要指示。石家庄市委、市政府高度重视，在外地出差的省委副书记、市委书记车俊接到报告后立即做出指示:要求不惜一切代价抢救被埋群众，同时注意现场抢救人员的安全防护，防止发生次生危害；认真查明原因，及时向社会公布。石家庄市委副书记、市长艾文礼及市委、市政府有关领导第一时间赶到现场，迅速组织有关部门和300多名公安、消防人员展开现场抢救工作，人员救治、稳定和善后处理工作有序进行。<br />
<br />
据透露，石家庄当日为雷电天气，河北省气象台和石家庄市气象台7时发布了雷电黄色预警信号，8时左右，石家庄暗如黑夜。9时左右，雷电交加，大雨倾盆而下。大量人员涌入屋内避雨。<br />
<br />
据现场三名目击者称，9时15分左右，突然听到一声雷响，只见一直径约一米多的耀眼火球击中房屋西北角，瞬间房屋自北向南依次倒塌，将避雨的人员埋在废墟中。<br />
<br />
事件发生后，石家庄市委、市政府立即组织防雷及建筑施工质量专家进行技术鉴定――<br />
<br />
从石家庄市气象局观测组记录看:石家庄8: 04-9: 52出现雷暴天气；<br />
<br />
河北省气象台闪电定位资料显示:石家庄东北角在9: 10-9: 15之间有过3-4次闪电记录；<br />
<br />
从现场情况看，倒塌房屋附近没有其它高大建筑物保护，房屋本身位置是该地较突出位置；<br />
<br />
倒塌房屋系在建建筑，还没有安装相应的防雷设施；建筑物圈梁和水泥预制板有大量钢筋，房屋内比较潮湿，极易吸引雷电，但没有好的泄放通道；<br />
<br />
由于第一现场已因为救人而破坏，没有找到雷击点，但从现场剩磁测试情况看，附近剩磁为2. 0mT、2. 3mT，屋顶剩磁为7. 8 mT。超过1. 5 mT的标准值5. 2倍。<br />
<br />
综合以上情况分析，专家初步判定闪电(球形)击中建筑物是建筑物倒塌的主要原因之一。<br />
<br />
官方称，倒塌房屋为砖混结构，临时避雨人员较多，无目击者确定被埋人数，给救援工作带来一定难度。截止到19时，现场抢救工作已经结束。共救出20名被埋人员，其中17人死亡，3人受伤，正在医院中救治。目前受伤的3人已得到有效救治，无生命危险。遇难者善后处理已全面展开，当地群众情绪和社会秩序稳定。(完)<br />
