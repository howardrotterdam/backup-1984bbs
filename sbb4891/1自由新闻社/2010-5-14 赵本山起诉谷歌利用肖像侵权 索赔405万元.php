																											<div id="postmessage_615585" class="t_msgfont">核心提示：赵本山此次状告某网站和谷歌信息技术(中国)有限公司，称某网站多个页面中出现卡通肖像Flash广告，该卡通肖像与他本人的肖像具有极大相似性。赵本山要求两家被告公司立即停止侵权、赔礼道歉，并赔偿其经济损失及合理开支共计人民币405万元。<br />
<img src="http://www.cqnews.net/yl/yl5/200901/W020090125576283751725.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
法制晚报5月14日报道&nbsp;&nbsp;因不满知名网站某社区使用了自己的卡通肖像作为Flash广告,用于推广产品,著名笑星赵本山将某网站和谷歌两家公司告上法院,并要求赔偿经济损失405万元。<br />
<br />
今天上午,海淀法院公开审理了此案。<br />
<br />
据了解,赵本山此次状告某网站和谷歌信息技术(中国)有限公司的原因,是两家公司联合开发的一款产品。<br />
<br />
赵本山起诉称,从2009年5月起,该网站多个页面中均出现带有赵本山卡通肖像的Flash广告,推广新产品。<br />
<br />
赵本山认为,该卡通肖像与他本人的肖像具有极大相似性,结合该卡通肖像所搭配的旁白表述等,普通人很容易辨识出该形象是他的卡通肖像,认为该卡通人物的主要作用就是为产品做宣传。<br />
<br />
最终双方对簿公堂。赵本山要求两家被告公司立即停止侵权、赔礼道歉,并赔偿其经济损失及合理开支共计人民币405万元。<br />
<br />
上午赵本山并未出庭,其代理人认为,两家被告公司未经赵本山同意,在网站广告中以卡通形式使用其肖像宣传推广其产品,严重侵犯了赵本山的肖像权。<br />
<br />
针对赵本山的巨额索赔,某网站认为,卡通图片不是肖像,不属于民法保护范围,而且该公司也没有使用该卡通图片进行营利性活动。因此并未侵犯赵本山的肖像权。<br />
<br />
<br />
谷歌公司则认为,涉案图片并非赵本山的肖像,而且谷歌公司也并非是网站的经营者,没有参与、使用图片,不应承担责任。<br />
<br />
为证实此次巨额索赔不是漫天要价,赵本山的代理人出示了与这位笑星及太极集团的代言合同,称税后的代言费为400万元。<br />
<br />
“两者没有关联性,也没有可比性。”某网站立即反驳称,这份合同约定由赵本山拍摄电视广告片以及平面广告,而且两年内不能再代言同样产品,而该公司使用的图片只是在自己的网站上,也未牟利。<br />
<br />
截至记者发稿时,此案仍在进一步审理中。<br />
<br />
