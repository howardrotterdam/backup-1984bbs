													<h2>中共高层同床异梦 要求薄熙来不要搞红色恐怖</h2>
						
						
																											<div id="postmessage_302113" class="t_msgfont">姜维平　自由亚洲电台　2009年10月26日&nbsp;&nbsp;&nbsp; &nbsp; &nbsp; &nbsp;&nbsp;&nbsp;<br />
<br />
正当参加世界报业协会的众多媒体为薄熙来涂脂抹粉之时，以中纪委副书记刘峰岩为首的中央第三巡视组，根据中央纪委、中央组织部的统一部署，于近日抵达重庆市开展巡视工作。此事发生不过10天之后，武警重庆总队的领导班子忽然做了调整，以打黑除恶出名的王立军不再独掌大权，京派干部董书民任政委，显然，自己比谁都黑，比谁都贪的政治局委员薄熙来已露出了马脚，并在中共高层新一轮权斗中失利，很可能成为继陈良宇之后，又一个落马的大贪官。<br />
<br />
露了马脚<br />
<strong>据重庆新闻界消息人士称，中央巡视组组长刘峰岩在和重庆官员见面时说，打黑不能搞运动，不能搞红色恐怖，更不能砸烂公检法</strong>，不能人人过关，现在必须把前一阶段反腐打黑的工作清理一下，已抓的人要走法律程序，该判的判，该放的放，而且领导干部首先要问一问自身做的怎么样？他还在见面会上，对即将开展的巡视工作做了具体布署。市委书记薄熙来听了，十分生气，他知道，针对汪洋和贺国强的打黑运动，虽然欺骗了老百姓和海内外舆论，但骗不了共青团派，何况，房峰辉又用大炮与刺刀制服了参加17届4中全会的中央委员，太子党预谋夺权彻底失败，他的所谓&quot;被逼打黑论&quot;终于露了马脚，只好假意接受批评，他代表市级领导班子讲话时说，挖到文强就到此为止了吧！下一步要看上面什么意见！据重庆新闻界消息人士透露，刘峰岩打断他的话说，这些以后再谈，你们只谈谈态度吧！。。。。。。接着中央纪委、中央组织部巡视工作办公室主任强卫东就如配合巡视工作提出了要求，还介绍巡视组副组长宁延令及全体成员与王鸿举、陈光国、张轩等市领导见面。<br />
<br />
另据重庆日报说，刘峰岩在会上指出，这次巡视的主要任务是对市级领导班子及其成员以下几个方面的情况进行监督检查：一是贯彻执行党的维护稳定的路线方针政策的情况，特别是贯彻落邓小平理论、&quot;三个代表&quot;重要思想以及科学发展观的情况；二是在打黑过程中执行民主集中制的情况；三是执行党风廉政建设责任制和自身廉政勤政的情况；四是开展作风建设的情况；五是选拔任用干部的情况；六是需要了解的其他事项。巡视期间，巡视将主要采取听取工作汇报、列席有关会议、受理信访、召开座谈会、民主测评、问卷调查方式开展工作，时间初步安排三个月左右。<br />
<br />
重庆新闻界的朋友对我说，薄熙来如今已是危在旦夕，他不服胡锦涛和习近平，又操控不了军队搞政变，只能靠当年读新闻研究生时的同学著文吹捧，大造舆论，弄巧成拙，进退两难，如何能爬上去呢？他还说，薄熙来现在转弯已来不及了，因为在重庆得罪了太多的人，下一步只有调走，才是唯一选择。但是又上哪里去履新呢？<br />
<br />
另据重庆日报说，薄熙来在会上表示，各级干部都要&quot;积极主动地&quot;向巡视组汇报情况，反映问题。要畅通巡视组与重庆的接触联系渠道。对巡视组发现的问题，要抓好整改。其实，谁都知道这是阳奉阴违，言不由衷。<br />
<br />
<br />
<br />
据 10月23日的重庆日报报道，喻林祥上将受胡锦涛的委托宣布命令，张轩讲话，董书民少将任武警重庆总队政委。22日，武警重庆总队召开机关和支队级单位主官干部大会，宣布了武警重庆总队领导班子成员最新调整命令。重庆消息人士说，这使自我感觉良好的薄熙来十分震惊，他气恼地拒绝参加会议。<br />
<br />
据报导，武警总部政委喻林祥上将、市委副书记张轩、副市长刘学普等出席会议，大会由武警重庆总队总队长常建民大校主持。喻林祥上将代表国务院、中央军委宣布了关于武警重庆总队领导班子成员调整的命令：武警重庆总队政委张家万少将退休，由武警总部后勤部副政委董书民少将接任；武警重庆总队政治部主任卢成元大校退休，由武警四川森林总队副政委暴玉怀大校接任。张轩代表市委市政府向来渝检查指导工作的武警总部政委喻林祥上将一行表示欢迎，向董书民、暴玉怀两位新到任的同志表示祝贺，并向张家万、卢成元两位为重庆武警部队建设和平安重庆建设作出巨大贡献的同志表示感谢。<br />
<br />
比对不久前，市委副书记张轩对重庆武警的评价说法，令人困惑。他虽然在这次会议上说，武警重庆总队在今年开展的&quot;灭枪治暴&quot;、&quot;打黑除恶&quot;专项斗争中发挥了重要作用，不愧是重庆发展、人民安居的保护神，官兵们的英勇表现将载入史册。还表示，希望武警重庆总队在他的领导下，从严、从难、从实战需要出发训练部队，不断提高官兵执行多样化任务的能力，严防爆炸、劫持、袭击等破坏活动，抓好反恐力量体系建设，全面提高部队建设水平，为维护社会稳定、服务经济发展、构建平安重庆作出新的贡献。但他对王立军的态度变的很暧昧。<br />
<br />
人们不会忘记，今年6月25日，打黑除恶行动正式宣布启动10天后，国务院、中央军委任命王立军兼任武警重庆市总队第一政委、第一书记。这一职位任命被外界认为是他全面接管重庆警界的开始，当时正处于打黑除恶斗争抓捕犯罪嫌疑人的关键时期。但是时隔不久，不料在这次会议上，被外界吹得神乎其神的王立军，并未出现，上述报道中，也只字未提王立军的名子，这一点颇为耐人寻味！那么以后谁是一把手呢？是王立军还是董书民？<br />
<br />
后院起火<br />
与此同时，在薄熙来苦心经营长达近20年的大本营辽宁省大连市，全国党务公开工作座谈会忽然落地召开了，显然，10月份并非海边滨城旅游的佳机，大连新闻界的消息人士说，薄熙来的后院着火了！这次会议非同小可，虽然省人大副主任，共青团派宋勇被捕上了报纸，那是上面故意释放的证明其公正反腐的烟幕弹，因为落马的原省纪委书记王唯众，原大连副市长宋增彬等多人，无一不是薄熙来的死党，他当年在大连提拔重用的市级领导干部刘长德，董文杰，成城等人都已撤职，副市长孙广田正在接受调查，只有夏德仁还表面得势，任市委书记，但他90年代后期才与薄熙来相识，与原省委书记闻世震私交也很好，所以他并非是薄熙来的死党。<br />
<br />
同一个新闻来源说，这次会议其实是一个摸底吹风会，自此辽宁省将对薄熙来进行全面的调查，其重点一是其老婆谷开来自80年代初既以权谋私，通过律师所大肆受贿的问题；二是恂私枉法，任期内排斥异己，裁赃陷害，制造了&quot; 天天渔港案 &quot;，&quot;陈德惠案&quot;，&quot;扬庆典案&quot;，&quot;张步宁案&quot;，&quot;刘晓滨案&quot;，&quot;高姿案&quot;等10几起冤假错案；三是包庇纵容黑社会头子邹显卫，董海波，李永建等人，杀人抢劫，巧取豪夺，恶贯满盈；四是包养情妇于某，扬某，李某，生活糜烂，道德败坏，等等，并彻底清理他过去的班底与死党的历史罪行，以便在中共18大召开之前彻底解决他的问题。<br />
<br />
据大连日报披露，10月21日至22日，中共中央党务公开工作领导小组在大连召开全国党务公开工作座谈会。中央纪委副书记、中央党务公开工作领导小组副组长张毅出席会议。省委常委、市委书记夏德仁出席会议并致词，省委常委、省纪委书记王俊莲，市委常委、市纪委书记刘爱军及全国31个省（区、市）党务公开工作主管部门负责同志，党的基层组织党务公开联系点的负责同志参加会议。<br />
<br />
报导说，张毅指出，深入推进党务公开，是推进权力公开透明运行、健全党内监督制约机制、加强反腐倡廉建设的必然要求。要充分认识新形势下推进党务公开工作的重要意义，进一步增强责任感和紧迫感。夏德仁在讲话中也透露了玄机，他说，大连反腐倡廉要围绕经济中心、服务发展大局，进一步落实惩防体系建设规划，加大监督检查力度，坚持依纪依法办案，积极探索创新教育的形式和方法，坚持日常监督与专项治理相结合，认真解决损害群众利益的不正之风，进一步规范党员干部的从政行为，全面贯彻落实好此次会议精神，以党务公开新成果推动大连反腐倡廉建设取得新的成效。<br />
<br />
由此听出了弦外之音，所谓的党务公开会议不过是一个遮人耳目的幌子，反腐与内斗相结合则是藏而不露的主题，薄熙来成了他们下一步深挖肃贪的目标。<br />
<br />
刘峰岩的个人简历：1963年入河北大学哲学系学习，1966年6月加入中国共产党。1968年7月后，任天津新华中学教师、校团委书记，河西区教育局团委副书记，1976年10月任中共天津市河西区委组织部副部长，1979年5月任天津市河西区革委会办公室副主任，1980年5月任天津市河西区委常委、河西区副区长。1986年5月后，任中共天津市河西区委副书记、书记，河西区区长，1990年任中共天津市委常委、市委宣传、思想工作领导小组组长，1993年5月任中共天津市委常委、市纪委书记，1997年在中共第十五次全国代表大会上当选为中央纪律检查委员会委员。此后一直在纪检部门工作，党的16大是中纪委委员，而后17大则因查处陈良宇有功而提升为中纪委常委，副书记，可见他不仅是老共青团派，而且是胡锦涛的得意的有功之臣。<br />
<br />
北京新闻界的朋友对我说，刘峰岩过去最精彩的行动是按照胡锦涛和共青团派的旨意，斗垮了陈良宇，2006年9月，他牵头的中央巡视组发现和查办陈良宇的案件，轰动一时，他今日这次重庆之行，估计是肩负重任。俗话说，来者不善，善者不来，刘峰岩早有证据，稳操胜唬辽宁和重庆南北呼应，前后夹击，薄熙来必将露出贪腐原形。<br />
<br />
但我认为，陈良宇与薄熙来有异同之处，他们都贪腐严重，证据确凿，都正当盛年，喧宾夺主，以咄咄逼人的气势指向胡温，对他们构成了威胁。但不同之处也相当明显，薄熙来是太子党大佬，近期他为了化险为夷，打击政敌而反腐打黑，骗取了许多民意，这一点是对胡温的挑战，也是洗牌的困局，将考验他们的内斗智谋。<br />
<br />
恰好辽宁省委书记张文岳既将退休，这又为共青团派提供了选用精兵强将，捣毁薄熙来老巢的最佳良机，假如走马上任一位与太子党势不两立的官员，他与原辽宁省纪委副书记，现大连市代市长李万才联手行动，将成功地揭开薄熙来家族贪污受贿，海外转移财产的惊人内幕，接下去应当立即通过媒体详尽公布他的罪恶，才会平息民众的误解，但江泽民等人一定会力挺薄熙来，不知是否还有变数。<br />
<br />
