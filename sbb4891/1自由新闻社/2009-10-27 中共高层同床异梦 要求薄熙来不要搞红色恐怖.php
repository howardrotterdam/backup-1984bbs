													<h2>周永康批示重庆打黑行动 称其为民心工程</h2>
						
						
																											<div id="postmessage_302239" class="t_msgfont">这条新闻放在这应该有相得益彰的效果~~~~<br />
--------------------------------------------------------------------<br />
<br />
历届党委政府高度重视打黑除恶，重大涉案团伙受到毁灭性打击主城区110接警量下降40%，重点涉黑案件力争明年春节前全部审结<br />
<br />
<br />
重庆打黑案审判视频集　　人民群众拍手称快，中央领导高度肯定。今年6月展开的全市打黑除恶行动受到了各方关注。目前进展如何？取得哪些阶段性成效？昨日，市委副书记张轩向市级各民主党派、工商联、无党派代表人士以及市级有关部门负责人通报了相关情况。<br />
<br />
　　检察机关共受理移送审查起诉涉黑涉恶案件36件327人。<br />
<br />
　　杨天庆、谢才萍、黎强、李义、张波、刘钟永为首的6个黑社会性质组织已被提起公诉。<br />
<br />
　　岳村、陈坤志、陈明亮、王兴强、邓宇平等5个重大涉黑案件已经移送检察机关，将在近期提起公诉。<br />
<br />
　　重大涉案团伙受到毁灭性打击<br />
<br />
　　张轩首先通报了今年全市打黑除恶的基本情况。她说，今年6月以来，为贯彻落实中央政法委打黑除恶的要求，推进“平安重庆”建设，由市公安局摸排爱丁堡小区命案入手，拉开了我市打黑除恶行动的大幕。市委对打黑除恶工作高度重视，市委常委会先后四次召开会议专题研究部署，成立了以市委常委、政法委书记刘光磊为组长的打黑除恶专项斗争领导小组。<br />
<br />
　　这次打黑除恶专项斗争，由市领导小组统一协调指挥，政法、纪检监察等多条战线联合出击，形成整体合力。其中公安机关先后成立了245个专案组，发挥了打黑除恶主力军作用。<br />
<br />
　　自我市开展打黑除恶专项斗争以来，截至10月25日，累计破获各类刑事案件2328起，涉及58种罪名，其中侦破故意杀人案24件；抓获涉案人员2915人，刑事拘留384人，逮捕1567人(其中，检察院批准逮捕的涉黑涉恶犯罪嫌疑人700人)；缴获各类枪支79支(其中军用枪支11支含2支冲锋枪、其他枪支68支)，子弹1482发，手雷4枚，管制刀具221把，冰毒62.01公斤；查封、冻结、扣押涉案财产17.43亿余元。长期盘踞我市的重大涉案团伙受到毁灭性打击，陈明亮、龚钢模、陈坤志、岳村、黎强、王天伦等重大黑社会性质组织头目无一漏网，160多名骨干成员全部被缉捕归案。<br />
<br />
　　主城区110接警量下降40%<br />
<br />
　　张轩说，打黑除恶专项斗争破获了一大批积案、命案，社会治安得到有效整治。今年1~9月，全市共破获各类刑事案件70577起，打击处理28274人，同比分别上升27%和5.4%；已破获积案27198起，同比上升52.9%，破获积案命案超过过去五年总和，现行命案破案率达91.35%。专项斗争以来，主城区110接警量下降40%，群众安全感明显上升。<br />
<br />
　　同时，打黑除恶专项斗争也为全市经济发展创造了良好的社会环境，维护了正常的经济秩序，有力地推动了经济建设。1~9月，全市经济发展势头良好，一批在全球有影响力的企业如HP等落户重庆。<br />
<br />
　　张轩说，我市的打黑除恶专项斗争获得了人民群众的欢迎。巴南区、璧山县、南岸区、九龙坡区群众高举横幅，手捧锦旗先后到市、区两级党委政府和公安机关，感谢党委、政府的英明决策，感谢公安机关伸张了正义。打黑除恶专项斗争也得到了市人大代表和政协委员的肯定，受到中央领导和上级机关的高度关注，获得了积极评价。9月25日，中共中央政治局常委、中央政法委书记周永康专门作出批示，称“打击、铲除黑恶势力，是让老百姓过上安定日子的‘民心工程’。近期在重庆市委、市政府的领导下，政法机关加大工作力度，见到了明显效果，为人民群众办了一件好事、实事。”<br />
<br />
　　历届党委政府高度重视打黑除恶<br />
<br />
　　黑恶势力严重危害社会稳定，历届市委、市政府对此都高度重视。张轩通报说，直辖以来，张德邻、贺国强、黄镇东、汪洋等历任市领导都高度重视打黑除恶，历届市委、市政府将经济社会发展和社会稳定摆在同等重要的地位，坚持不懈组织开展打黑除恶，有力维护了社会稳定。在历届市委、市政府的高度重视和坚强领导下，我市根据中央政法委和公安部的部署，打黑除恶不辍不息，持续推进，在2001年、2006年先后掀起了声势较大的打黑除恶专项斗争。全市政法机关打掉了一批黑社会性质犯罪组织，判处了一批涉黑犯罪分子，查处了一批“保护伞”。<br />
<br />
　　重点涉黑案力争春节前审结<br />
<br />
　　对我市打黑除恶下一步工作，张轩通报说，市委将认真贯彻中央领导指示精神和中央政法委的部署，确保打黑除恶专项斗争取得人民群众更加满意的成效。<br />
<br />
　　与此同时，我市将督促政法各部门始终把案件质量放在首位，确保每一起案件都事实清楚、证据确凿、定性准确，经得起法律和历史的检验。明年春节前，力争将目前已进入司法程序的全部重点涉黑案件审结。此外，将深挖黑恶势力的“保护伞”，斩断其经济命脉，彻底摧毁黑恶势力滋生的政治和经济基础。继续加大侦办积案和现行重特大案件力度，加强政法队伍建设，探索建立打黑除恶专项斗争的长效机制。据重庆日报消息<br />
<a href="http://news.sina.com.cn/c/2009-10-27/023016503285s.shtml" target="_blank">http://news.sina.com.cn/c/2009-10-27/023016503285s.shtml</a></div>

							
							
							
							
