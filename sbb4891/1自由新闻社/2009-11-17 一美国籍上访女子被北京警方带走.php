&nbsp; &nbsp;&nbsp;&nbsp;一小时后我在美国接到家里人的电话：“士亮被抓了”，我不相信这是真的、我不相信我所热爱的中国会发生这样颠倒黑白的事情。4月6日至30日，我因肺栓塞先后在深圳、香港接受治疗，经医生建议6月10日我返回美国继续治疗，此时正在美国治疗的我听到这个消息犹如晴天霹雳。尽管医生提醒我有百分之三十的患者可能会在长期乘坐飞机时导致病情复发而猝死，我还是不顾医生和家人的劝阻，6月26日，离美国经香港、深圳转到合肥，于6月30日下午疾奔五河县公安局，向警方求见刘士亮，但被拒绝，办案人只是同意我留下在深圳将刘士亮因被打成重伤（颅内淤血、颅骨骨折）而遵医嘱用的药。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;刘士亮为何吃药、我又为何说刘士亮的被抓是一个具有颠倒黑白性质的事情，我还要从2007年5月14日说起。<br />
&nbsp; &nbsp; <br />
<br />
谁才真正非法侵入他人住宅<br />
<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;2007年5月14日中午，与刘士亮同村的刘士勋之子刘廉杰，在我婆婆家的树上掏喜鹊窝，正值我大伯哥刘士洋的儿子刘启东放学回家，制止了他。刘廉杰回家后觉得委屈，就向其父哭诉。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;当时，刘士勋在其当警察的堂弟蔡广映的姐姐家帮助建房，见儿子哭诉，便率妻子刘会荣、父亲刘善月、堂哥刘士才和刘廉杰等众人先后到我婆婆家和刘启东家，扬言说非要弄死刘启东不可，由于找不到人，便将刘启东家的门锁和玻璃打碎。之后又闯进我二伯哥刘士展家，在厨房将二伯嫂按倒在地暴打，我侄儿刘家肖欲保护母亲，被喝了酒的刘士勋父子俩拖到门外暴打，13岁的刘家肖满脸是血，后在医院缝了4针，并被诊断为轻伤。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;我婆婆随即向110报警，双忠庙派出所警察一小时后到达现场，我家人强烈要求以寻衅滋事和非法侵入他人住宅罪将刘士勋等人拘留，却被警官张磊、朱光学告知：他们只有权力让受伤者去医院，其他问题要由领导决定。刘士勋的堂弟蔡广映，2007年由部队转业至五河县朱顶镇派出所，（刘士勋的小祖父在蔡湖村落户改姓蔡），在蔡的阻挠下，该案至今不被受理，受伤妇孺的伤情也没有得到依法鉴定。<br />
&nbsp; &nbsp; <br />
<br />
警察不作为、刘士亮被打成重伤和我们的婚事流产<br />
<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;2007年5月，我和刘士亮相约各自回到出生地办理结婚手续。我回到了美国办理手续，5月13日我公公刘善同电话告知，家里已请人装修新房，但第二天下午我又被电话告知：家里被人砸了。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;刘士亮电话询问双忠庙派出所，但派出所无所作为，他当日打电话给蚌埠市公安局督察队，市公安局让他与县公安局直接联系。刘士亮向县公安局督察队反映情况，被告知“要先了解一下情况，二十分钟后给你回电话（有通话单为凭）。”<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;5月15日早8点，我未婚夫刘士亮与同事陈远志、袁福波、蔡兆宣以及同学姜宁、欧明好、张兴昆一起回双忠庙刘蔡村家里，路遇刘士勋和其堂兄刘士才，刘士亮质问刘士勋为何殴打妇孺，刘士才上前挡住刘士亮高喊：“都出来，原谅子（刘士亮）带人来了”。刘士亮说：“我不是来打架的，我只想问问刘士勋为什么打女人和小孩”，这时刘士勋跑到他家的猪圈后面拿了一根两米长的木棍，边挥舞木棍边大喊：“都出来给我打！”<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;出于防卫，刘士亮夺下刘士勋挥舞的木棍，刘士勋摔倒在地。这时刘士亮的同事说：这家人不讲理，咱们赶快走吧。刘士亮扔下木棍转身欲走，但被刘士勋的老婆从背后拦腰抱住，刘士勋接过其父刘善月递过来的一根短木棒向刘士亮的后脑猛击，刘士亮应声倒地。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;就这样一个是非分明的事情，在我们多次告状后，五河县公安局才在半年以后的2007年12月对刘士勋予以刑事拘留，五河县检察院在12月25日对其批准逮捕。<br />
&nbsp; &nbsp; <br />
<br />
五河县公安局如此歪曲事实<br />
<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;2008年2月15日，五河县公安局回覆刘士亮的代理律师，以下文字录自信函：<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;案件事实：2007年5月14日，刘士勋家与刘士洋家因小孩发生纠纷而打架。刘士亮（刘士洋弟弟）在深圳听说此事后而于5月15日乘飞机带陈远志、袁福波、蔡兆宣到合肥，在合肥又邀集姜宁、欧明好、张兴昆共7人租乘两辆轿车到双忠庙镇刘蔡水刘。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;车子停在水刘庄西路上，刘士亮带上述6人窜至刘朝阳家找刘士勋（当日刘朝阳家建房，前日刘士勋在其家干活），刘朝阳说“刘士勋不在”的情况下，刘士亮带人进入刘朝阳家屋内找刘士勋没有找到，后离去从刘朝阳家西卷子朝北窜至刘士勋家。（以上事实刘士亮仅承认带6人，不承认到刘朝阳家找人而是遇到刘士勋，但有刘朝阳的110指挥中心出警登记为证。）<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;刘士亮进入刘士勋家院内，当时刘士勋及其父刘善月、妻刘会荣在厨房内吃饭，刘士亮及另外一人进入厨房内将刘士勋拖出屋外，在邻居刘士军家屋前殴打，致刘士勋、刘会荣轻微伤。（以上刘士亮否认进入刘家屋内而是在外遇到找刘士勋谈谈，但有刘士勋、刘善月、刘会荣证词及证人刘士才证词和法医坚定为证，此案已立非法侵入他人住宅案另案处理）。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;刘士亮等人将刘士军家电话线搞断后准备离去，刘会荣撕拽住刘士亮，刘士勋从刘善月手中拿过木棍捂刘士亮头部一棍，刘士亮被同伴架着逃上车内逃跑。在路上感觉头部受伤，电话告知其姐夫曹大好，送往省立医院抢救，经检查系右颌急性硬膜外血肿，伤情构成重伤。（上述事实刘士勋不承认，但有受害人指认、法医鉴定、欧明好、蔡兆宣、袁福波证词及辨认笔录为证，经路人陆贤栋亦证实一38岁左右男子持捂了一年轻人，双忠庙派出所也证实下午4时左右刘士亮方报警被打伤送往合肥）。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;综上，刘士勋故意伤害他人身体，伤情构成重伤，已触犯了《中华人民共和国刑法》第二百三十四条规定，构成故意伤害罪，应当追究刑事责任。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;2008年2月15日<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;调查人；刘展屈友龙<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;从调查报告的字面上看，警方调查人刘展、屈友龙就已为日后报复刘士亮埋下了伏笔，他所采用的证人证言均系刘士勋的亲属，谁能想象刘士亮既然准备去打架却不拿任何打架的工具，谁能想象刘士亮“邀集”（法律术语应是纠集）的六个“打手”竟然没有一个人出手帮助刘士亮去打刘士勋及其家人，这几名所谓的打手也没有一人受伤。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;另外调查报告中还说：我的未婚夫刘士亮曾闯入过刘士勋的住宅并扯断电话线，可是负责调查此案的警官既没有对现场进行勘验，更没有对所谓的“扯断电话线”进行指纹鉴定。没有这样的证据和刘士勋家亲属外的证人证明，他们就如此进行有罪推定，实在是拿法律当儿戏。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;综上所述，正是因为五河县公安局的不作为和偏袒，导致了刘士亮的重伤和被诬告。其幕后黑手就是刘士勋的堂弟蔡广映和县公安局系统内部，也正是因为此人的策划，才有了刘士亮今日之悲剧和我的上访。<br />
&nbsp; &nbsp; <br />
<br />
谁为我们未完成的婚姻负责？<br />
<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;我于2007年5月15日获得美国德州州务卿威廉姆斯的亲笔签字证明，我的婚姻状况是未婚。5月17日中国驻休斯顿领事馆官员童启兰以2007休领字第008799号的确认书，认定了州务卿的签字属实。上述两个文件为我和刘士亮的婚姻奠定了中美双方的法律基础。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;1998年我在美国参与编辑《Let’s go China》，6月来中国实地考察，此时距离我1989年第一次来中国已有近十年了。我喜欢中国的风景名胜，喜欢这里的人民。刘士亮是我在修编再版此书时偶然遇到的最热心帮助我的中国人，此故事已经《知音》、安徽公共电视台等多家媒体报道，我在此不多赘言。我真心爱着刘士亮、刘士亮也真心爱我，这是不容质疑的事实。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;刘士亮被打住院，出院后刚两周，依照中国法律，我俩去安徽省民政厅办理结婚登记手续，当时民政厅负责登记的公务员告诉我：希望刘士亮康复之后再来登记（因刘士亮当时头部作了大手术，影响结婚照的形象），我们决定08年上半年再到合肥领取结婚证。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;但08年2月，经五河县公安局局长吕兴亚批准，刘士亮在不知情的情况下被网上通缉。为此，我曾向中国法律人士咨询，他们告诉我只有最大恶极和重大民事纠纷的责任人才可能被上网通缉。五河县公安局明知刘士亮在深圳工作的单位、地址及通讯方式，滥用公权，不追究真正非法侵入他人住宅和暴打妇女儿童的刘士勋等人的法律责任，却把脏水反泼到被打成重伤的刘士亮身上。如此颠倒黑白，令人触目惊心！<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;刘士亮被网上通缉和遭逮捕前后，我和刘士亮家人及亲朋、律师托人希望保释或尽快开庭，同时，我也去了中国国家公安部、最高人民检察院、中央纪监委、安徽省省公安厅、省检察院、省人大等机关上访，有关媒体也给予了关注，但直到逮捕后的三个多月，暨今年的9月14日才开庭审理。之后一个半月，也没有再开庭或宣判，难道此案是重大复杂案件，而有所顾及吗？<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;尊敬的领导，如果说刘士亮真的非法侵入过刘士勋的住宅，那也是刘士勋伙同其父刘善月、其妻刘会荣及兄弟刘士才入侵我家房屋暴打妇孺在先，如果中国法律是公正的，当以同样的罪名追究刘士勋、其父刘善月、其妻刘会荣及堂兄刘士才的非法入侵罪责。况且此案只听刘士勋家人的说法，别无他人能够证明。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;刘士勋被五河县法院依刑法判处有期徒刑五年后，二审给刘士勋加刑六个月，并取消了附带民事的赔偿。刘士勋家人在二审后扬言：我堂弟蔡广映现在是领导，早晚会给我们报仇，我们打了刘士亮也是白打一分钱咱也没赔，刘士亮家告也没用，告来告去早晚也要抓了刘士亮。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;我伯兄刘士洋多次向县公安局反映刘家父子非法侵入我家住宅，办案人视却以此罪将我未婚夫逮捕。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;尊敬的领导：五河警察在深圳抓走刘士亮后，刘士勋家人说：别看刘士亮找了个美国媳妇，没什么了不起，在五河也得老老实实地由蔡广映所长摆布，她敢告状，公安局有的是招儿整治刘士亮。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;许多美国朋友和中国朋友在得知我们的不幸后，劝我召开国际记者招待会来揭露五河县公安系统存在严重的不作为和乱作为问题，也有朋友指点我以美国的方式、中国的法律向公安部申请在中国北京的天安门广场抗议示威。但是出于对中国的热爱我暂时没有选择那样做，因为我不希望自己因此成为另外一种新闻人物。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;我第一次随母亲来到中国，就对中国投入了巨大的情感，我也深知中国在通往现代化的路上还有许多工作要做。也正是因为我热爱中国才希望看到中国不仅仅在经济上有着巨大的进步，同时我也希望在法制、人权上能有更多的改变。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;为了刘士亮的冤情，为了法律的公正，也为了中美两国人民的友谊，您能否在百忙之中依法过问一下此案呢？<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;我不不希望让五河县司法腐败的问题，成为奥巴马本月访问中国，中美最高层的人权话题。我希望尽快看到你们给我的回信，及依法公正的答复，祝你们工作愉快。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;美国公民朱莉（Julie Harms）<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;2009年11月2日</div>

							
							
							
							
