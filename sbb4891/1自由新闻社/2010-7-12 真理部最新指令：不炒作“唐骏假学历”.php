　　我们在此还原一个真实的唐骏。<br />
“最愉快的时光”<br />
　　唐骏1984年毕业于北京邮电学院应用物理学专业，次年留学日本名古屋大学电气电子专业，但未获博士学位，终止学业，转去美国发展。<br />
　　1994年进入微软是唐骏职业经理人生涯的起点。他在微软的第一份工作是程序员，后升为产品经理。他在自传中回忆，称经过了八轮面试才得到一份在Windows NT开发部门做程序员的工作。<br />
　　唐骏的工作是帮助微软发展Windows中文版。在多位微软在职和离职员工看来，这不是一个需要博士学位才能获得的职位，如果是加州理工大学的博士，甚至可以说大才小用。<br />
　　在此时和之后的很长时间，同事们并未听唐骏称自己为“加州理工大学博士”。一位曾在微软工作多年的资深人士认为，微软人事部门在入职时的审查很严，唐骏在进入微软的简历中没有必要“作假”，因为这个工作他出国之前的北京邮电学院的学历就足够了。<br />
　　唐骏也不会提及西太平洋大学博士学位。他是在翌年即1995年才获得这个学校颁发的博士学位证书。<br />
　　“这个工作他应该完成得不错，因为不久他得到了提升。但如果将之归结为唐氏开发法，就太夸张了。” 另一位曾参与视窗开发的前微软中国区总监也证实，视窗开发是上千人的团队。<br />
　　唐骏的下一个职位是Windows NT开发部门的高级经理。随后，微软决定在中国设立一个技术支持中心，唐骏主动请缨。1997年年底，唐骏被总部派到上海，负责组建大中华区技术支持中心，为中国大陆和港台地区的用户提供技术支持服务。<br />
　　“这是一个类似于售后服务的机构。需要一点技术，但主要是管理。微软当时需要有人来收集和归类整理中国客户反映的问题，帮助解决，并反馈给产品部门以修改缺陷。”前述微软中国资深人士介绍，这对唐骏是一个很大的提升，亦是对他之前工作能力的肯定。<br />
　　唐骏之后的表现没有令总部失望。他用了不到三年时间，将一个区域技术支持中心升格为全球技术中心，团队也由最初的25人扩展到后来的400多人。<br />
　　当时的唐骏给人留下深刻印象。“技术中心第一天成立的日子是1997年11月4日，也是我第一天加入微软的日子，我还记着唐骏穿着一件草绿色的微软T恤。”一位唐骏的老部下回忆说，当时30多岁的唐骏用英语演讲，“充满激情”，给了这些刚入职的年轻人一个“Surprise（惊奇）”。<br />
　　这些年轻人并不了解唐骏，他们只知道这位领导“曾在总部做得很优秀，然后被派遣回中国，再早，他在中国、日本和美国获得学士、硕士和博士”。<br />
　　在他的前部下看来，“微软牛人太多了，平时没人会把学历当回事。”话虽如此，这只是有漂亮学历的人的想法，在海外求学多年却没能拿到名牌大学博士学位的唐骏或许仍视之为软肋。他曾在不经意间对一位同事提起自己是名古屋大学的博士，在得悉该同事认识其导师时就密嘱手下不要再提及此事。<br />
　　不少曾与唐骏在上海共事的人，后来也在微软或其他公司走上管理岗位，多年之后，对他仍保留不错评价，认为“他善于激发员工的潜能，从他身上学到很多东西”。<br />
　　“在上海微软的那几年，是他成长的时期，也是我们成长的时期。”前述唐骏的老部下认为，在唐骏所有职业生涯当中，“可能最留恋的就是这一段”。<br />
　　这的确也是唐骏职业生涯中最轻松惬意的时期：他管理了一支年轻的技术团队，成员对他尊敬有加，业务流程相对简单，几年运营下来早已驾轻就熟。<br />
　　 2001年，比尔・盖茨亲自到上海为微软亚洲技术中心升格为全球技术中心揭牌。“对唐骏在支持中心的工作，所有微软人都会给予很高的评价。”前述微软离任高管告诉本刊记者：“能把上海支持中心做成亚洲的、又做成全球客户的问题都由你来解决，这是一个很大的成绩，从团队管理到团队文化在公司都被认可。”<br />
内斗牺牲品<br />
　　2002年3月20日，微软（中国）有限公司总裁兼总经理高群耀突然宣布辞职，一周后，微软正式任命唐骏为高群耀的继任者。一直梦想“从一个技术管理者转变成为一个独立部门管理者”的唐骏，得到了人生的一个大机会，也是大挑战。<br />
　　这是一个新的提升，因为实际管辖范围从过去的单纯技术部门一下拓展开来，从业务战略、市场销售到政府关系、法律公关事务及内部行政，中国区总裁都有责任。但熟悉微软内部政治的人都为唐骏感到担忧。<br />
　　“尽管表面上‘微软中国总裁’是一个非常光鲜的位置，但大家都知道那个位置不好做，上面还有一个大中华区总裁，是一个很不好相处的台湾人。唐的前任就是与台湾人斗争失败才离开。”一位微软老员工对本刊记者回忆了数年前那场最终导致唐骏出局的内部纷争。<br />
　　临危受命之后，微软为唐骏召开了场面颇为盛大的媒体见面会，隆重推出唐骏，唐骏给人的印象还“有些生涩和拘谨”。<br />
　　所有到会的记者，都拿到一份唐骏的个人简历――<br />
　　“1962年出生；1981年考入北京邮电学院；1985年留学日本；1990年赴美攻读博士；分获物理学学士、电子工程学硕士和计算机科学博士学位。1994年进入微软公司，担任微软总部WindowsNT开发部门的高级经理；2001年升任微软全球技术中心总经理。三次被授予微软公司的最高奖项：比尔・盖茨总裁杰出奖和杰出管理奖。”<br />
　　随着管辖范围的拓宽，管理的复杂程度也成倍增加。但中国区总裁在微软不是一个很高级的职位。在唐骏之上，离总部之间还隔着大中华区总裁、亚太区总裁。大中华区总裁除了管台湾和香港市场外，就是管中国区总裁，两个职务权限严重重叠。<br />
　　不仅如此，由于当年中国区层级较低，层层都有人管，总部也有很多条线交叉共管。“体系非常复杂，业务的横纵线、来自各个方面的要求都比较多，再加上又是全新的环境，他当时的压力可想而知。”前述上海微软的员工向本刊记者表示。<br />
　　唐骏走马上任后，也继承了与时任大中华区总裁黄存义的明争暗斗。接近唐骏的人士透露，唐骏当时非常痛苦，私下抱怨“老板（指台湾人）很坏，员工欺骗他”，处境颇令人同情。<br />
　　另一位接近微软中国高层的消息人士向本刊记者透露，微软总部当时还设有中国管理委员会。这一委员会并无实权，但对中国区事务有建议之责。时任微软全球副总裁、微软中国研究院院长的李开复正是委员会负责人。他曾为此给盖茨写信，批评中国区的叠床架屋结构不合理，建议改变。之后，李开复又向微软CEO鲍尔默引荐了摩托罗拉公司副总裁陈永正。<br />
　　在唐黄争斗激烈的2002年、2003年间，微软中国区的销售业绩一度比预期有相当大的下滑，这帮助微软下了最后的决心，提升中国区的级别。<br />
　　2003年8月微软任命陈永正为微软副总裁及微软大中华区首席执行官。唐骏身份不变，改向陈永正汇报，黄存义则以区域销售市场总监身份也向陈永正汇报。同年10月18日，微软中国对外宣布了新的管理架构，以进一步实现权力向陈永正集中。微软中国区总裁唐骏的权限收缩为仅负责微软全球技术中心以及设在北京的中国技术中心的工作。唐骏回到了两年打拼的原点。<br />
　　“本来李开复建议陈永正开掉黄存义而留下唐骏，但陈选择把黄调开、把唐骏开掉”，前述接近微软中国高层的消息人士向本刊记者透露。唐骏在这个名存实亡的“微软中国区总裁”位置上又坚持了两个月后，向陈永正请辞，前提是要求获得“微软中国名誉总裁”。<br />
　　多位熟悉微软的业内资深人士认为，唐骏时期的微软中国区总裁在微软内部级别并不高，唐骏在微软时期的多个职位并不需要有高学历，基本上还是靠能力干起来的，特别是在上海从无到有打造了全球技术支持中心，还培养了一批年轻人，反而是到任北京后的业绩无足称道，最终是“因销售业绩不佳，内部摆不平才被迫走人”。<br />
　　在唐骏之前，医护人员出身的吴士宏亦曾担任过微软中国区总经理一职，亦足证明此职位无需高超学历和技术背景，只要过去资历证明有管理能力即可。<br />
寻找光环<br />
　　多位唐骏的前同事公认，唐骏的变化，发生在从上海到北京之后，特别是在离开微软前后。“讲的话有些太荒谬了，什么四大发明，他这些事情对我们影响很大，大家整天当笑话讲。”<br />
　　很多人猜测“他是总经理的工作太难做了，压力太大，太痛苦”，以至于剑走偏锋。这是一种善意的理解。事实上，“忽悠”在当今的中国商场上不仅不是缺点，反而是无往不利的“通行证”，在商场上，吹嘘自己拥有“神秘高干背景”、“强大靠山”以及“超强资金运作能力或实力”的人比比皆是。与全球技术中心总经理单纯管理一群技术人员不同的是，唐骏在“微软中国总裁”任上交往面急剧扩大，曝光机率明显增多，原本就爱“吹点小牛”的唐骏在这种普遍的浮夸中膨胀起来。<br />
　　另一方面，在业内资深人士看来，唐骏的变形与他的危机感亦不无关系。上世纪90年代进入跨国公司管理层的大陆籍职业经理人并不多，时势机遇以及关键时刻的眼光和努力，使唐骏搭上了头班车。但在随后的职业升迁中，他直面的是学历背景深厚又比大陆人更熟谙跨国公司内部政治的港台籍职业经理人的挑战。唐骏急于用更大的光环来包装过去，迎接未来。<br />
　　2004年2月3日，微软（中国）有限公司宣布，“唐骏不久将由公司退休，同时将被授予该公司‘荣誉总裁’的称号”。荣誉总裁只是光环的一部分。六天之后，唐骏宣布加入盛大网络发展有限公司（下称盛大），与过去犹抱琵琶半遮面的“美国博士”不同，这次伴随而来的是“双料博士”的头衔。<br />
　　为了庆祝唐骏加盟，盛大在北京召开了主题为“群星璀璨，共铸辉煌”的媒体答谢会，正式宣布唐骏出任公司总裁。席间最有趣的场面莫过于唐骏先是从盛大董事长陈天桥手中接过聘书，继而又从陈永正手中接过为唐骏特制的“微软中国名誉总裁”的荣誉证书。陈天桥更承诺“唐骏将全面负责公司运营与管理”。<br />
　　事实上，此时的陈天桥，心中早已经有了向“国际化大公司”迈进的第一个目标：赴美上市。他需要一个可以用英语跟华尔街投资人沟通、又能淡化企业家族色彩的职业经理人作为形象包装。<br />
　　唐骏在微软公司任职期间，其对外发布的个人简历中，对具体在哪个大学获得博士学位始终语焉不详。而转投盛大后，盛大公司在向美国SEC递交的IPO报告书中赫然写到：“唐骏拥有美国西太平洋机电工程学博士学位（a doctorate degree in electrical engineering from University of Pacific Western）以及日本名古屋大学的电子学博士学位（a doctorate degree in electronics from Nagoya University, Japan），此外他也拥有名古屋大学的电子学硕士学位（a master’s degree in electronics）。”<br />
　　这是唐骏的公开简历中首次提及其具体毕业的院校为“西太平洋大学”，此时的唐骏，显然已经从一个不知其具体出处的“计算机专业博士”进化成了电子学和机电工程的“双料博士”。2004年7月，唐骏在出席中国卡通产业论坛时，其公布的简历更摇身一变为“加州理工大学计算机专业博士学位”。这是本刊记者查到的最早关于其“加州理工大学博士”的记录。<br />
　　“盛大是一个家族式的企业，陈天桥虽然长于对政府层面的公关，却并没有信心亲自到国外去做上市路演，所以他在上市前一个月请来了唐骏。”一位不愿透露姓名的盛大离任高管向本刊记者回忆说，当年陈天桥邀请唐骏加盟盛大的待遇很优厚，除了职位还包括不菲的期权。正在微软内部权争中痛苦挣扎的唐骏如沐甘霖。<br />
　　双方一拍即合，唐骏初到盛大的第一年还算忙碌，他的确为推动盛大在美国纳斯达克的成功上市尽力。但随后的三年中，唐骏又被闲置了。<br />
　　在众多盛大普通员工的眼中，这位总裁在进入公司的四年中，除了负责“融资、路演和处理与风投的关系”以外，并没有负责更多公司具体的管理事务。“也曾有几个项目是向他汇报，但最后不了了之。”有盛大集团中层员工这样向本刊记者描述。<br />
　　前述盛大离任高管告诉本刊记者，最初唐骏也曾很有热情地想办法做一些管理工作，诸如梳理会议流程、决策流程之类，但很快意识到，在盛大这样一个典型的家族企业中，“他的这些努力结果只能是适得其反”，随后唐骏便很识趣地选择了“靠边站”。而陈天桥也不只一次对高层私下表示，唐骏在他眼里不过是一个“高级公关经理”。<br />
　　总结在盛大的就职经历，唐骏曾表示，“最自豪的一点是找到了职业经理人在民企的生存法则”。<br />
　　这段在外人看来光鲜的经历对于唐骏其实并不愉快。2008年6月4日接受“鲁豫有约”采访时，唐骏曾在介绍自己的唐氏减压法时，坦陈在盛大时压力很大，“于是我问自己，如果我不当盛大的总裁了会怎么样，不会怎样。两个星期后，我会去做另外一个公司的总裁。”<br />
　　不过，由于陈天桥请其出山时曾许下260多万股股票期权，在解禁期结束前，唐骏暂时还不能离开，他也在等待另一个总裁的机会。<br />
　　2008年5月，46岁的唐骏，以中国IT行业著名职业经理人的身份，辞别盛大，转投于传统领域一家并不算“明星企业”的企业――福建新华都集团，出任总裁兼首席执行官。加入新华都，也将刷新唐骏作为职业经理人的高薪纪录，据唐骏自己透露，包括现金加股权，他在新华都的薪酬总计10亿元。<br />
　　两度跳槽使唐骏更醉心于增加知名度。唐骏本人曾在《跟唐骏学管理》一书中对外传授经验称：“我深知在中国，‘知名度就是生产力’”。从忙着出席各种论坛发表演讲到忙着写自传、写职业经理人的管理心得，再到写电影剧本。近年来，因为爱讲能讲，他是国内各种峰会论坛活动热衷邀请的企业家之一。“中国打工皇帝”的形象及成功经历时时见诸报端，成为年轻人学习的楷模。<br />
　　与之相伴的，则是“加州理工大学博士”头衔。即使这并非唐骏本人发明，但在各种公开和私下场合，唐骏显然对此受之泰然。<br />
　　就在接受“鲁豫有约”采访的第二天，唐骏出席了北京邮电大学“唐骏孙春兰奖学励学基金”的启幕仪式，北京邮电大学校长面对台下学子介绍唐骏时，即称他“在加州理工大学获得博士学位”。<br />
　　唐骏的自传《成功可以复制》是目前公众用以了解他的主要渠道。在第一版的叙述中，亦明确载有同样的话。而从第二版中，人们获知唐骏的经历为：“唐骏1980年考入北京邮电学院，四年后获得应用物理专业的学士学位；1985年申请公派到日本名古屋大学，在电气电子专业攻读语音识别领域的研究生课程。1990年，当进入到攻读博士学位阶段，在一次确定博士论文研究方向的讨论中，唐骏与导师发生争执，最终他并没有在日本获得博士学位，而是终止了学业转去美国发展。”<br />
　　在豆瓣网中针对唐骏的自传《我的成功可以复制》（下称自传）五星评级中，32.4%的网友给了最高的“五星”，有43.7%的网友给了四星。在评论栏，很多人留言表示“受益匪浅”，更有人留言说：“在目前我所处的阶段读到这本书，可以说是一种幸运。”<br />
