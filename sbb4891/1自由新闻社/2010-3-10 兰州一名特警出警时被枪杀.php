<br />
　　3月9日晚8时10分，警方部署对付晓霖的抓捕行动，由付晓霖的哥哥付某某敲开房门，市公安局特警支队民警实施抓捕。不料，房门刚打开，付晓霖持枪负隅顽抗，特警大队民警窦勇在此过程中遭枪击致颈部受伤，送省人民医院抢救无效后牺牲。<br />
<br />
　　事发后，正在甘南检查工作的省委常委、省委政法委书记、省公安厅厅长罗笑虎委托省公安厅政治部主任冯延前往慰问民警家属，并要求省市公安机关全力做好缉捕工作，防止犯罪分子脱逃。目前，省、市公安机关警方正在全力展开追捕工作。<br />
