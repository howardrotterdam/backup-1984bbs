 <br />
<br />
　　3月9日下午4时50分，市公安局指挥中心接到渭源路派出所汇报，当日下午4时40分，家住南滨河东路198号201室的刘某到该所报案，称3月8日下午4时，其丈夫付晓霖(男，汉族，现年39岁，张苏滩蔬菜市场商贩，吸毒人员，陕西口音身高1.70米)怀疑刘有外遇，两人在住处发生争执后，付从卧室取出一把枪，并击发一枚子弹，但未击中刘。刘趁机离开住处报警，现付仍在住处。<br />
<br />
　　接报后，指挥中心迅速指令相关单位赶赴现场处置。城关分局局长张江武、副局长陶云山、李勇宝率分局特警大队及刑警大队技侦民警相继赶到现场，开展先期核查、警戒工作。<br />
