													<h2>兰州一名特警出警时被枪杀</h2>
						
						
																											<div id="postmessage_510710" class="t_msgfont">　中新网兰州3月10日电 (记者 侯志雄)今天上午10时，甘肃省兰州市公安局宣传处处长甘守义在接受记者采访时说，截至目前，枪杀特警的犯罪嫌疑人尚未抓获，兰州市公安局已在全城实施围捕，现场有200多名特警、刑警在兰州市城区多处设点进行围捕，武警也参与配合了此次行动。 <br />
 <br />
<br />
　　兰州警方10日发出书面悬赏通告说：2010年3月9日20时许，兰州市发生一起枪击案，犯罪嫌疑人作案后畏罪潜逃，为迅速侦破案件，兰州市公安局决定对其公开悬赏抓捕，希望广大市民积极提供破案线索，抓获犯罪嫌疑人，对提供有价值线索破获案件的，奖励人民币10万元。<br />
<br />
　　通告还说，犯罪嫌疑人付晓霖，曾用名付小林，男，汉族，1971年5月6日出生，陕西省西安市长安区人，身高1.70米左右，体态偏瘦，操陕西、河南口音，会说普通话，有吸毒史。<br />
<br />
　　据警方提供的信息说，付晓霖案发前在兰州市城关区张苏滩蔬菜批发市场做生意。3月9日，付晓霖因怀疑其妻刘某有外遇而对其进行了殴打，刘某到兰州市公安局城关分局渭源路派出所报警。<br />
<br />
　　兰州市公安局接报后，即组织民警前往其家中调查处置。在调查过程中，付晓霖突然开枪将民警窦勇击伤后，逃离现场，民警窦勇送至医院后抢救无效牺牲。当日晚，甘肃省公安厅政治部主任冯延前往牺牲特警窦勇家中，对其家人进行慰问。<br />
<br />
　　3月10日9时，甘肃省公安厅在全省公安系统召开紧急电视电话会议，通报了案情，在甘肃13个市州进行布控，全力围捕歹徒。兰州市公安局局长王幸，副局黄大功正在现场指导案件处理工作，组织警力抓捕犯罪嫌疑人。<br />
<br />
　　兰州警方提供消息还称，被枪杀的民警窦勇现年40岁，1986年参加工作，系兰州市公安局特警支队民警，二级警督，因破案得力数次立功。<br />
<br />
　　当地媒体报道称，犯罪嫌疑人付晓霖因妨碍公务，于2007年9月5日被兰州市城关分局刑警十一中队刑事拘留，羁押于兰州市第一看守所，2007年12月25日出所。2008年7月23日付晓霖威胁其女友(现妻子刘某)使其跳楼，于2008年7月26日被城关分局刑警五中队刑事拘留，后于2008年8月10日取保候审。</div>

							
							
							
							
