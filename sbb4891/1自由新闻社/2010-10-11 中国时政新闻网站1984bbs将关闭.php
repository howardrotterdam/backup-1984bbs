																											<div id="postmessage_866611" class="t_msgfont">小众新闻自由网站1984BBS将被迫在明天关闭<br />
<br />
上海特约记者 曹国星<br />
<br />
中国大陆以反抗新闻审查知名的网站“1984BBS”年初被GFW屏蔽，并遭来自中国大陆的不明身份的网络对其所在境外机房攻击。<br />
近日，其创办人“张书记”面临着北京警方的威胁，并遭便衣警察限制人身自由，其创办两年的“1984BBS”面临被迫关闭的前景。<br />
1984BBS主要发布被中国大陆的互联网审查机关删除或者禁止发布的新闻，聚集了中国大陆不少活跃的媒体人，并经常有传媒编辑将其受到的“真理部”禁令在这里共享发布。<br />
由于网站话题敏感，实行的是小圈子的邀请码制度，访问者必须输入帐号密码方可访问。话虽如此，网站仍很快受到“有关部门”关注，并被列入“GFW”的屏蔽名单，在管理团队的努力下，几经迁址，仍然可以以“HTTPS”模式加密访问。<br />
今天，“张书记”透露说，10月8日下午，两名北京市公安局国保警员再次找他“约谈”，话题是“1984bbs和刘晓波获奖”。<br />
据称，在这次谈话中，北京市公安局的国保警员表示，“接下来都是自己的选择”，如果坚持做1984bbs，“将有一条路等着我”。对此，张书记说，“我不知道这是条什么路，但看一些异议人士的遭遇，我知道这条路会很艰辛。”<br />
在甘家口派出所进行了近两小个半小时的约谈后，他乘警车回家，楼道里一个鬼鬼祟祟的便衣在我进家门前把头伸出来，确认了他家的房间号。<br />
从这天起，“张书记”被限制出门。他家楼梯口有三个“退伍兵”轮番站岗，防止其未经许可外出。这些人要求，如果“张书记”要外出，必须给“所在地的片警打电话请示，片警要向甘家口派出所请示，甘家口派出所要向市局国保请示”。<br />
在这些人的“随扈”下，“张书记”现在不能走出所在的社区，只能“申请”在社区内走动。<br />
之前，派出所要求他之前的房东赶他搬家，他所在的公司在北京警方威胁下对他进行“离职处理”。 失业在家的张书记在北京外国语大学报了一门英语课程，由于被限制人身自由，他甚至无法前往学校上课。<br />
今天（10月11日），张书记对外透露说，北京公安局的国保开始调查他的妻子，他妻子的父母，甚至是祖父祖母的情况。<br />
“不知道他们接下来要做什么。”他说，“为了新闻自由，我做1984bbs可以让我自己受尽艰辛，但不想连累我的爱人和她的亲人。”<br />
在1984BBS站内发出的公告中，张书记说，“我不知道我还能坚持多久，如果我放弃1984bbs，你们会理解吗？”<br />
今天下午，他对本台记者表示，如果没有意外，为了保护自身和家人的安全，他被迫于将于明天关闭1984BBS。<br />
