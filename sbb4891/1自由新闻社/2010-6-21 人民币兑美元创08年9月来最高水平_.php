<br />
外资银行驻上海的交易员说，最初市场考验上周五收盘时的水平，而后市场将重点放在人民币升值的近期前景上。目前几乎所有人都在卖出美元。<br />
<br />
他补充说，除非央行干预，否则美元/人民币盘中可能测试6.8100元，目前在自动撮合交易系统央行还没有采取行动。<br />
