<br />
　　北京一家关怀同性恋者团体同行工作组的成员郭子阳介绍说，武装警察曾在两、三个月前对牡丹园采取过类似的“打击卖淫”突袭行动，但这次行动的警力规模之大前所未闻。<br />
<br />
