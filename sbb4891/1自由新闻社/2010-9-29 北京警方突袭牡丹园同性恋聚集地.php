　　报道说，警察和特警队员在星期天晚间乘坐约２０辆警车来到牡丹园，每辆警车上有四名警察。警方还在9月27日夜间再次对牡丹园采取了突查行动。<br />
<br />
　　事发当晚曾被警察带走的一名男同性恋者星期一对《环球时报》说：“你无法想象，在这次事件中，我们有超过２００人受到了多么深的创伤”。报道引述网上发帖者提供的信息说，在牡丹园逗留的男同性恋者被带到花园路警局，被要求出示身份证件和接受血液检测，也被拍下照片、留下指印。<br />
<br />
　　北京市公安局一位发言人表示，警方在周日和周一晚间对牡丹园采取的行动，只是在国庆假日到来前全市年度治安检查的一部分，被带走的人只要没问题就会获释。这位发言人没有确切说明被抓走的人违反了哪些法规。<br />
<br />
　　事发时从牡丹园被带到警局的人已在当天午夜陆续获释。在本周一的下午，牡丹园内的情景又如往常一样，在树丛浓密的小山上，可见一些男子相互调情、聊天或独自站立，周围还有使用过的安全套和纸巾。<br />
<br />
　　一名曾常去牡丹园的35岁男同性恋者对《环球时报》说：“我不得不说这是一个杂乱的地方，不同年龄、不同素质以及带着各种想法的人都混杂在这个‘天堂’里”。这名男子还说，在平常的周末晚上，来牡丹园的男同性恋者可能多达３００人，许多人在这里发生性行为，包括性交易，一些人在性活动中不采取安全保护措施，还有一些抢劫偷盗者利用受害人不敢暴露身份的心理而下手。<br />
<br />
