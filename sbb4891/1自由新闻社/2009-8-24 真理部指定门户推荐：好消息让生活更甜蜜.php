<br />
　　董&nbsp;&nbsp;力 <br />
<br />
&nbsp; &nbsp; 2009年08月24日00:00&nbsp;&nbsp;来源：人民网-《人民日报》<br />
<br />
　　法新社日前报道，由于觉得媒体上负面新闻太多，美国佛罗里达州的一个12岁男孩马克斯・琼斯创办了“今日周末新闻”网站，专门报道令人鼓舞的新闻。当前，该网站每天的浏览量已经达到5000人次,人们在这里还可欣赏到琼斯撰写的评论。<br />
<br />
&nbsp; &nbsp; 媒体研究表明，西方一些媒体长期热衷报道负面新闻，受众对此日益厌倦。2006年，英国BBC网站所做的一项有关英国人幸福感的在线调查显示，许多英国人认为，媒体报道了太多的负面新闻，给受众提供的是“由恐惧、担忧和疑虑组成的大餐”。对社区犯罪、政府失职的渲染，对政治人物的嘲弄和菲薄，以及对全球变暖等危机性话题的夸张描述，“让英国人感觉很糟糕”。<br />
<br />
&nbsp; &nbsp; 美国的雅虎网站也曾就新闻对社会心理的影响做过一个在线调查。结论是，很多美国人觉得媒体报道了太多消极的东西。有的网友说：“媒体让人们觉得世界正在分崩离析，疾病、战争、环境恶化……这些消息让一个原本快乐的人变得压抑和沮丧。”一些人干脆说自己现在已经不看新闻。有人质问：“以前那些有关我们身边的英雄的报道都哪里去了？”<br />
<br />
&nbsp; &nbsp; 参加这两项调查的网友都感觉，在负面新闻铺天盖地的情况下，新闻看得越多越不开心。美国俄亥俄州立大学的研究人员则发现，心情沮丧的人很难发现生活中的美好。厌倦了负面报道泛滥的西方民众开始希望改变这一局面。参加BBC在线调查的英国人提出，“限制媒体对本地及全球的负面新闻的报道”，“建立一个记分系统，规定每家媒体报道正面新闻和负面新闻的比例，如果负面新闻超出比例，这家媒体就要被罚款”。<br />
<br />
&nbsp; &nbsp; 一些人以传播给予人们信心与温暖的好消息为使命,已经开始自己开创传播正面新闻的渠道。在英国，早在1993年，退休女演员莎娜・巴罗斯就创办了一份名为《正面新闻》的报纸，传播“人们勇敢地为美好生活而奋斗的故事”。现在，这份报纸在英国和美国发行，还在西班牙和阿根廷建立了办事处。在美国,不仅有琼斯的“今日周末新闻”，一家叫做“好消息公报”的新闻网站做得更专业，内容更丰富。<br />
<br />
&nbsp; &nbsp; 英国一位资深媒体人士感叹：“世界上好消息多得是。新闻不必全部是恐怖主义、战争和经济衰退。一批新媒体正在争着给读者提供鼓舞人心的报道，这些报道可以让生活更甜蜜。”</div>

							
							
							
							
