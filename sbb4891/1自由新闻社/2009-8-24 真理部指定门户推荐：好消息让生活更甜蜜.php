媒体研究表明，西方一些媒体长期热衷报道负面新闻，受众对此日益厌倦。2006年，英国BBC网站所做的一项有关英国人幸福感的在线调查显示，许多英国人认为，媒体报道了太多的负面新闻，给受众提供的是“由恐惧、担忧和疑虑组成的大餐”。对社区犯罪、政府失职的渲染，对政治人物的嘲弄和菲薄，以及对全球变暖等危机性话题的夸张描述，“让英国人感觉很糟糕”。<br />
