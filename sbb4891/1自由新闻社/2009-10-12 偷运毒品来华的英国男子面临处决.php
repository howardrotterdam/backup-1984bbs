																											<div id="postmessage_278478" class="t_msgfont">偷运毒品来华的英国男子面临处决<br />
<br />
一英国男子因在两年前携带价值25万英镑的海洛因进入中国而被判死刑。他有可能成为第一个在中国被执行死刑的英国公民。其律师说，他有严重的精神疾病。<br />
&nbsp; &nbsp;&nbsp; &nbsp; <br />
 <br />
2004年4月7日，中国温州警方押解一组重刑犯接受公审大会审判。（图片来源: STR/AFP/Getty Images）<br />
<br />
 <br />
<br />
一名英国男子因被判定走私海洛因到中国而即将被执行死刑。该男子名为阿克马尔・谢赫（Akmal Shaikh），现年53岁，原籍北伦敦地区，据称其在被发现在手提箱中携带4千克价值25万英镑的毒品后遭到逮捕。<br />
<br />
据说谢赫患有严重的精神疾病，他将成为首位在中国被处决的英国公民；其律师警告说，他将被枪决。外交部官员称，上周有报道说他的第二次申诉已被驳回，但尚未得到“官方证实”，中国当局也没有发布任何消息。<br />
<br />
据《观察家》得到的电子邮件透露，在有波兰、吉尔吉斯斯坦和塔吉克斯坦罪犯参与的一次精心设计的行动中，谢赫被吸收为新成员。他辩护说自己受该团伙欺骗，并不了解毒品。<br />
<br />
谢赫和一名英国女子结婚，有五个孩子，他真诚地相信那帮人是他的朋友，要培养他成为流行音乐明星。但事实上，他的律师和朋友们都说，他曾经患有妄想性精神病，现在仍然受到病痛的折磨。<br />
<br />
尽管已经得到他患精神疾病的证据，但是中方拒绝对这名英国人进行心理评估，也没有考虑到他的精神疾病。<br />
<br />
据审判旁观者称，谢赫在出庭为自己进行无罪辩护时语无伦次，时间长达50分钟，令法庭上的所有人忍俊不禁。<br />
<br />
 <br />
<br />
请求中国政府免除谢赫死罪的支持者中还有患燥郁症的男演员史蒂芬・弗莱。据悉，戈登・布朗在最近的匹兹堡G20峰会上已经向中国国家主席胡锦涛提到了谢赫的情况，不过，英国律师强调说迫切需要更多的外交压力。<br />
<br />
法律慈善机构“缓刑”的主任克莱夫・斯塔福・史密斯（Clive Stafford-Smith）代表谢赫说：“来自中方的最新报告相当令人担忧，如果不考虑影响阿克马尔的严重精神问题，他可能很快就会面对洞穿后脑勺的子弹。”<br />
<br />
谢赫被关在偏僻的西北城市乌鲁木齐。支持者称，他因患有精神疾病而被利用，是一个精心策划诡计的牺牲品。然而，就在五年前，他似乎还过着稳定的生活，和妻子在肯特镇成功地经营着一家微型出租车公司。谢赫的前律师说，在2003年，谢赫还是个“迷人的、有魅力的男人”。<br />
<br />
然而，不久以后，他的精神状况急剧恶化。该律师说，尽管没有经济能力，但他第二年离开伦敦去了波兰，打算在那里成立一家航空公司。“在他去波兰的时候，你甚至无法坐下来和他谈话。”<br />
<br />
在波兰和一位新女友的关系破裂后，谢赫的精神状况更加恶化。在要求匿名后，她称谢赫当时行为已经“十分愚蠢和疯狂”，例如他曾经在向她发送的一封虚假信件中声称其赢得了一百万英镑。<br />
<br />
2007年，谢赫在卢布林市认识了波兰海洛因贩子，似乎得到他们的帮助，当时他发给华沙英国大使馆的电子邮件暴露了他脆弱的心理状态。其中，谢赫声称自己曾经和报喜天使谈过话，还说如果允许他在卢布林召开记者招待会，他如何能够阻止7月7日伦敦爆炸案。<br />
<br />
他发给华沙大使馆工作人员的数百封电子邮件中具代表性的一封说：“嗨，老兄，你有棉花糖。我，男人，篝火，你必须烤些棉花糖，我是正常的。”另一封写道：“作为一名英国人，我是说哈罗德王，没有这样的事情，在眼睛里攻击爆炸中心。这不再只是不公平。”<br />
<br />
有些留言是发给74位个人和组织的，其中包括托尼・布莱尔、保罗・麦卡特尼、“父亲为正义”（Fathers 4 Justice）运动组织、苏格兰场、BBC节目Top Gear以及乔治・布什总统，在邮件中他被称为“Bushie&quot;。<br />
<br />
但是，这些电子邮件也记录了谢赫如何结识波兰罪犯，尤其是名叫卡洛斯的人物。他声称自己和卡洛斯编了一首歌，而且卡洛斯承诺他可以把这首歌灌制成走红的唱片。卡洛斯告诉他，自己与音乐商有极好的联系，他们会帮助他取得成功。2007年9月，卡洛斯为谢赫买了前往吉尔吉斯斯坦的机票。<br />
<br />
在那里，谢赫的护照被几个不知名的男人拿走，而他似乎很平静，因为当时他相信自己将要获得国际声誉，“就不需要它了”。后来，谢赫的护照连同一张去中国的机票被退还回来。他被介绍给一个名叫Okole的男人，并由他陪同前往乌鲁木齐。据谢赫说，Okole在中国经营一家大夜总会，他承诺可以让这位英国人在那里演唱自己的歌。<br />
<br />
2007年9月8日，他们动身前往中国，途中在塔吉克斯坦首都杜尚别逗留。在杜尚别，Okole通知谢赫，他不得不独自乘飞机去中国，因为机上只剩一个座位了。Okole给他一个手提箱，并保证乘下一航班跟来。9月12日，谢赫到达乌鲁木齐机场，在手提箱经过安检扫描后他被捕了。<br />
<br />
据悉，谢赫一直和警方全力配合并解释说手提箱不属于自己，他也不了解海洛因。他甚至“精心”组织了一个行动，让警官等Okole从塔吉克斯坦来。但Okole一直没有出现。斯塔福・史密斯说，“那些职业毒品走私贩很有可能知道他患有精神疾病，易于操纵。”<br />
<br />
被捕后两个月，谢赫被判处死刑。然而，直到一年以后的2008年11月，英国政府才得到通知。去年五月，谢赫对判决不服，在乌鲁木齐地方法院提出上诉，被驳回后，只好向最高人民法院提出最后上诉以挽救自己的生命。据报道，这一上诉现在也败诉了。第二次上诉被驳回后，“几乎可以立即”处决囚犯。<br />
<br />
他的兄弟阿克巴说：“阿克马尔多年来一直在挣扎，我们现在知道那是严重的精神疾病。当我们知道他无法正常为自己辩护时，都非常担心他的安全。他会非常迷惘和不安。我们祈求中国法庭会看出他精神不健全，免除他的死刑。”<br />
<br />
总部设在伦敦的法医心理学家、临床顾问彼得博士（Dr Peter Schaapveld）说：“如果此案发生在英国，心理健康问题将贯穿全部审判进程：他应该受到指控吗？应该被裁定有罪吗？他的病情应该减轻刑罚吗？”<br />
<br />
五个月前，彼得飞往中国，要对谢赫的精神状况进行评估，但是中方拒绝让他见谢赫。不过，英国外交部官员得到允许可以和谢赫谈15分钟。通过他们对谢赫行为的描述，彼得能够“99%肯定”地推断他患有某种精神错乱症，可能是燥郁症或是精神分裂症。<br />
<br />
