　　赵文卓对妻女呵护备至，全程均亲自忙前忙后照顾孩子，打点行李，而爱妻张丹露几次伸手想要帮忙，赵文卓都微笑回避。在等待行李的过程中，赵文卓还时不时地逗弄躺在婴儿车内的爱女，完全一副幸福男人的模样。<br />
