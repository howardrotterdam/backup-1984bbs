&nbsp; &nbsp;2008年7月，我毕业了。带着一丝茫然，一丝雀跃，我从育才走进了社会这所大学。如今，半年时间过去了，我经历了毕业后的所有酸甜苦辣。仔细想来我的经历也许能给即将毕业的或者说在大学中对未来充满迷茫的学弟学妹们一些经验教训，让你们少走一些弯路。 <br />
&nbsp;&nbsp;大四的时候，几乎所有的人都在为未来而忙碌，考研的考研，考公务员的考公务员，考村官的考村官，家里有关系的已经联系好了工作，没有找到的在寝室里长呼短叹或者避口不谈工作。我也加如了考研这支队伍，没想到也是万人过独木桥。虽然总分成绩过了但由于英语只考了40几分，所以不得不去C区比如新疆，贵州这些地方读。想想那些人生地不熟的地方就算了。于是从考研这支队伍加入了找工作这支队伍。 <br />
&nbsp; &nbsp;大四上学期的时候，学校曾组织到西师本部去参加了招牌会，但令人气愤的是人家一见是育才的就说“对不起，我们只招本部的”连面试机会都不给，即使给了录取的机会是少之又少。虽然毕业后我们拿的是西南大学的学位证，但现在的毕业生太多了。一个移动公司招营业员50名就吸引了1000大学生去应聘。其中不乏西师以及重庆各个高校的毕业生。经历了大大小小的招聘会，甚至有一个星期在饭店学厨，后来由于当时对证券比较感兴趣，应聘到一家证券公司担任投资顾问，名称虽好无非就是对别人说一些模棱两可的话。比如“据我个人认为，某某股票会涨有很好的投资前景，但大家知道股票这东西谁也说不清楚（这句话是最关键的）也是公司三天培训的结果”。也许是自己一直怀有一颗创业的心，或者说日复一日的生活对我来说没什么吸引力。工作两个星期后决定和朋友一起干汽配。当然未满一个月公司不给发工资，于是在为公司白干了两个星期后我走上了创业的道路。 <br />
&nbsp;&nbsp;创业的第一个经验：朋友归朋友，生意归生意。当时朋友已经干了一年汽配，叫我去跟他干1000一月主要是学学经验，帮帮他的忙。当时看在朋友的份上就答应了。一个半月后，不但没拿到一分钱经验什么的也没学到，因为任何人在生意上都有所保留，没有人会把经验给你（除了父母），这也许是我摔的第一个跟头。于是我萌生了自己创业的念头。 <br />
&nbsp;&nbsp;在经过一番思索后，我还是把起点放在了我们学校，毕竟这是我学习的地方。于是第二个跟头随之而到。我和寝室的几个哥们凑了一万多元钱准备收书和收棉被。由于都是学生我们把学生的利益放在了前面。于是学校出现了建校以来的怪现象，书价由5毛涨到了8毛，棉被由10元一套涨到10元一床。由此产生的直接后果是除了一部分书卖出去后，我们收的书除了运费一分未赚。这几万斤书可是我们起早贪黑有时甚至到凌晨两三点才收来的。最后就剩下了一屋的书。这也许不算一个跟头我们没亏却为毕生生们卖回了一个好价格，他们的东西没有贱卖。我们用去的只是力气和时间。但是棉被却让我摔了第二个跟头。当时联系的厂家叫我们去收，收多少要多少，但后来却以质量不好为借口拒收我们的东西，面对一屋的棉被我们是无言以对。离校的时间到了大家都要各奔前程了，现在最后悔的是当时因为这个没有在毕业的时候好好和兄弟们聚聚有些人也许是一辈子都不能见面了。后来因为怕货下雨发霉就开始重新找买家，但是这样的话人家就趁你急的时候使劲压价在没有办法的情况下我们以10元买进8元卖出，我也学到了第二个经验：生意上口说无凭一定要签合同。 <br />
&nbsp;&nbsp;后来经过努力，办了文化经营许可证在学校开了一家淘书阁，虽然在大校门外地理位置偏僻但由于价格便宜，同时我们也根据同学的要求可以代买代卖生意也勉勉强强。女朋友要考试，两个人在一起生活得也算甜蜜。但也许是命运的作弄人生的第三个跟头迎面而来。 <br />
由于开的是书店，我们一直想进一批文具来卖，但刚开始的时候没钱，毕业后在外找工作不但没钱反而贴了几千元钱。开店的钱还是两个人以前存的和在外找家教赚的钱，没有向家里要钱。开了两个月后就想进点文具，也许真是命运的安排让我们多受点教育，那天校门口来了一两面包车，他们自称是为学校送货后的赠品，有笔，笔记本，篮球等用品。谈了价格我们认为相对公道就清点货物，他们是老手我们是什么都不懂，他拿出来的都是好的我们随手拿了一盒也是好的就没有再看其他的了。最后一共是6200元钱，当时没那么多钱还是向同学借的想卖完了还钱，他们拿钱走了我们再清货的时候才发现上当了，不是数量有问题就是质量有问题。发现后我们报警了，警察说这种骗局在我们育才已经有几十起了每年都有受骗的，再说这个立不起案它不属于诈骗只是属于销售伪劣产品。现在我们只有打落牙往肚里吞，连家长都不敢说怕他们担心。自己做的事只能自己承担。面对6000多元的东西我们是欲哭无泪因为它花光了我们所有的积蓄还欠别人的钱。我们也不能把差的拿出去卖那样的话我们也是在骗人，现在就只能送出去了，我们想只要到我们淘书阁来买书的就送笔，买上5元就可以抽奖，奖品有笔，笔记本，伞，篮球，羽毛球，乒乓球等。我也想赚回6000元但我更知道不能一错再错。在这写下我的经历只希望大家以后不要上这种当，大多数人受骗了选择不说但我希望看到的人相互转告不要再让骗子骗我们育才学生的钱。希望大家以我为戒。也希望我们育才的学生以后一路走好。 <br />
&nbsp; &nbsp; 我不认输，我不服气，我要让上天知道我的勇气和决心！</div>

							
							
							
							
