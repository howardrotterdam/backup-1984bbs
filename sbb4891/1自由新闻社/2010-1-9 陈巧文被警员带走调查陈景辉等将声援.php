高铁正反立场电台掀骂战 工总孙启烈s碍经济发展 朱凯迪s重商轻民 <br />
<br />
【明报专讯】高铁拨款再度押后，支持及反对人士移师到电台烽烟节目继续交锋。为政策护航的工业总会主席孙启烈批评反高铁行动「阻碍经济发展」，又质疑行动倡议人朱凯迪「反政府」之目的，是要争取追随者；朱反指孙比一般人享有更大投票权，「声音大」却漠视民意。随着周五再商议拨款的日子逼近，预料双方阵营的辩论会继续升温。<br />
<br />
公共专业联盟主席黎广德昨日在出席港台节目《星期六问责》时，肯定周五反高铁的成绩，认为方案押后可带来一星期额外时间，让市民分析政府周五在压力下才肯公开的资料。出席同一节目的孙启烈批评，行动令香港停滞不前，当年港英政府欲建新机场也没有如此拖拖拉拉，「如要讲收回几多（钱）才兴建，现在仍在用启德」。<br />
<br />
黎广德s高铁报告较新机场差<br />
<br />
黎响应时指孙所引述的机场资料失实，当年方案仔细列出营运数据，但高铁数据则比机场的报告差。他又直指孙启烈「落后于形势」，「建议你离开座位，走入抗争人群中听他们的诉求，他们不是反对兴建高铁，而是希望政府改变重商轻民的思维」。孙未待他说完，立即接续说，「周五最多有4000多至1万人反对，但香港有700万人」。<br />
<br />
孙质疑藉反政府招追随者<br />
<br />
同接受访问的本土行动成员朱凯迪表示，未来一星期会尽力和市民沟通，让他们了解方案。但他直言，对本港政制感到沮丧，「香港人平日工作辛苦，未必有时间了解（高铁方案）；就算反对声音大，政府也可利用功能组别，在市民不知不觉下通过」。孙启烈听到这里，忍不住打断其发言，为功能组别护航。他强调，功能组别是历史遗留下来的制度，有其存在价值，「可平衡社会各方的利益」，朱凯迪则反驳s「特别是你的利益嘛，孙生？」孙为之一度语塞，然后说「我只是代表商业界，为香港经济利益发声」。朱凯迪质问，「那为什么你的一票大过我的一票？为什么功能组别多一票？」孙解释，「那是因为工业界创造就业职位」，双方火药味甚浓。<br />
<br />
朱指商界享特权<br />
<br />
经主持人调停，朱凯迪继续讲述未来一星期的任务，但孙启烈似仍未平复，突然再次插嘴说s「我觉得朱凯迪是因为无权无势，透过搞这些行为，反政府就有追随者，所以觉得好开心。」朱即反驳s「那我知道孙先生一向也很大声，政治权利亦比一般人多。」孙响应说s「我只是一介市民。」<br />
<br />
工业总会主席孙启烈（左）批评反高铁行动阻碍经济发展，又质疑行动倡议人朱凯迪（右）「反政府」之目的是要争取追随者，两人为此激辩。中为公共专业联盟主席黎广德。（李绍昌摄）</div>

							
							
							
							
