<br />
　　星岛环球网消息：纪念毛泽东同志诞辰116周年联谊会暨“关爱未来”系列公益活动启动仪式23日在北京钓鱼台国宾馆隆重举行。毛主席的保健医生、王稼祥同志的夫人朱仲丽、毛主席的女婿王景清、毛主席英文翻译、原外交部副部长王海容、毛主席秘书张玉凤、毛主席警卫处长曲琪玉等毛主席身边的工作人员、后辈共同聚首追忆毛主席，同时关爱未来。<br />
<br />
　　人民网报道，2009年12月26日是毛泽东同志诞辰116周年，为了更好地纪念毛主席，将毛泽东思想转化为具体行动，联谊会还特别启动“关爱未来”系列公益活动。解放军总后勤部原政委、全国人大环资委副主任张文台上将与中共中央统战部原部长、新中国第一任女省委书记万绍芬等人共同触摸水晶球，现场启动了“关爱未来”系列公益活动。港澳收藏家协会会长、著名收藏家周耀祺向“关爱未来”活动无偿捐赠3000多枚毛主席珍贵像章、60年来内地发行的所有与毛泽东主席有关的邮票、小型张和首日封等价值过亿元的珍贵藏品。<br />
<br />
