<br />
　　清晨，旭日东升，群山逶迤，居庸关上，一号烽火台已铺起红地毯。<br />
<br />
　　腰鼓打响，旗帜飘扬。两条长约60米、高近3米的黄色巨龙在一号烽火台与二号烽火台之间架起，仿佛在崇山峻岭中飞腾。<br />
<br />
　　上午10时正，仪式开始。<br />
<br />
　　中华人民共和国国歌、亚奥理事会会歌奏响。八名来自三军仪仗队的护旗手正步出旗。国旗与第十六届亚运会会旗飘扬在长城上。<br />
<br />
　　10时15分，中共中央政治局委员、广东省委书记汪洋宣布：第16届亚洲运动会火种采集开始！<br />
<br />
　　17名采火少女身穿白裙，沿长城的缓坡而下，一步一停款款走来。采火圣女康辰晨，带领手捧采火棒与火种盒的采火少女，进入采火仪式现场。<br />
<br />
　　10时23分，康辰晨举起手中的采火棒，缓缓屈膝，伸至凹透镜制成的太阳能“聚火盆”上。<br />
<br />
　　此时此刻，所有的目光都聚集在采火棒上。<br />
<br />
　　一秒，两秒……20秒左右，轻烟腾起，现场可以嗅到火种轻燃的香味。<br />
<br />
