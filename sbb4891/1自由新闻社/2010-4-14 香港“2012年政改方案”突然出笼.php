记者：陈沛敏、张嘉雯<br />
<br />
公民党党魁余若薇昨日指出，政府推出没有普选路线图的方案，全无诚意落实真普选，市民 5月 16日公投投票已是唯一出路，「如果 5月 16日，有大批大批市民排住队去票站投票，话畀政府听我O要废除功能组别，钐厍可能会听清楚民意。」<br />
