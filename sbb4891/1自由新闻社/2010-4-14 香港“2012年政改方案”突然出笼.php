													<h2>Central Walk：阿爷对普选特首态度有变</h2>
						
						
																											<div id="postmessage_571245" class="t_msgfont">Apple Daily 苹果日报网上版 －－2010-04-19<br />
Central Walk：阿爷对普选特首态度有变<br />
<br />
今次政府抛出政改方案，外界焦点集中于阿爷未有响应泛民提出普选路线图及取消功能组别的诉求。有泛民中人对单仁说，其实细心阅读方案内容，可以看到一些阿爷对香港政制发展态度的微妙转变，其中最明显的是对普选特首的转变。<br />
<br />
该泛民中人忆述， 05年政改争拗之前，泛民曾与阿爷的「中间人」讨论双普选问题，对方大多认为普选特首可以先行，因为安全系数较高，反而担心普选立法会后将被泛民控制逾半议席，冲击行政主导。故此， 05政改方案被否决后，人大常委提出所谓普选时间表，明确指出 2017年可以普选特首，之后可以普选立法会。其实人大常委会的决定没有提及 2020年普选立法会，那只是煲呔自行演绎，最快可以 2020年做到。由此可见当时阿爷对特首普选的态度是比较宽松的。<br />
<br />
收紧选特首提名限制<br />
<br />
然而，今年港府再推政改方案，特首选委会组成的建议较 05年出现大倒退，政府建议加入选委会的区议员仅 75人，远较 05方案建议全体约 400名区议员少。<br />
<br />
泛民中人分析称，以 05方案为计算基础，泛民候选人要在 1,600人选委会中取得 150个选委提名尚有可能。相反，今次方案只建议选委会增加 75个区议员，届时泛民随时连一席也拿不到。以梁家杰上次参选仅取得 132个提名为例，若 2012年要多拿 20个提名参选，将会十分困难。所以泛民有理由相信，新方案反映阿爷开始收紧特首选举提名限制，对普选特首极不放心。<br />
<br />
制度上卡住泛民参与<br />
<br />
阿爷为何忽然收紧对特首提名，泛民内有不同猜测。有人估计是中央鹰派抬头，既然不能收回 2017年普选承诺，就要在制度上卡住泛民参与，现在开始收紧选委会组成，日后可以「循序渐进」地提出一个高门坎特首提名方法，包括引入预选机制。该泛民中人称，此形势下，泛民要争取普选路线图又怎能乐观呢？</div>

							
							
							
							
