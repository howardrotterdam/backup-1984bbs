<br />
职工盟李卓人更建议温和民主派在争取对话的同时，亦应转为下月的「变相公投运动」助选，「姿态上要争取对话，不争取反而正中下怀，但亦希望温和民主派为『公投』助选，因为谈判需要筹码」。<br />
