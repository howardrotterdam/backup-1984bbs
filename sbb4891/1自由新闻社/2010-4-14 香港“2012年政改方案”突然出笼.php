													<h2>由希望到失望 普选联感受骗／文s李先知</h2>
						
						
																											<div id="postmessage_565421" class="t_msgfont">明报网上版 2010-04-15<br />
--------------------------------------------------------------------------------<br />
由希望到失望 普选联感受骗／文s李先知 <br />
<br />
【明报专讯】特区政府昨日突然公布政改修订方案，与原方案大同小异，并表明是终极方案，而人大常委会副秘书长乔晓阳也实时出来见香港媒体驻京记者，显示特区与中央是有默契地一致行动。政坛耳语透露，对于一直寻求对话的普选联成员来说，这俨如突如其来的变脸，令他们感到受骗，故此实时酝酿采取一些激进行动。不过，普选联成员杨森昨晚指出，他会在本周六的普选联内部会议中倡议继续要求与中央对话，以理性、据理力争的方法，争取2016年及2020年有最大成分的普选。<br />
<br />
知情者透风谓，近几个月，特区政府高层与一些北京派来香港做沟通工作的「特使」，一直有跟民主党和普选联核心成员接触，甚至到了本月上旬，这些接触仍然非常频繁。「诚恳」的实质对话令不少局中人到了本周初，还以为「对话」是会有成果的。其间，民主派的核心人物收到了如下的几个信息s<br />
<br />
（1）特区和中央政府都不愿意看到香港政局不断激化，令民主派全部倒向公民党和社民连的「变相公投运动」，特区政府希望争取大部分民主派议员支持政改方案，而非「夹硬」撬走4票勉强通过方案，因为这样无助于解决香港的政治僵局。<br />
<br />
（2）北京不可能马上公开承诺2020年取消所有功能团体议席，但可以研究其它方法，令公众相信中央确有诚意让香港迈向普选，例如可以考虑进一步增加分区直选和区议会功能组别的议席，从原建议各增5席变成各增10席，令有广泛选民基础的立法会议席达到64％，极接近三分之二多数，加上几位支持普选的功能界别议员，日后要进一步增加普选成分，便不怕传统功能组别议员为延续自身利益而否决。<br />
<br />
这与特首现时提出的方案框架完全一致，只是力度加大，把部分2016年才做的改革提前完成，形成民主派学者盼望的「路径倚赖」能够出现。另一个可以研究的建议是，中央授意下由工商界提出功能团体改良方案，显示就算是工商界保留功能组别特色的方案，也基本符合普合平等的原则，将来的普选不会是假的。<br />
<br />
（3）特区政府高层和北京「特使」还抛出了「大和解」构想，即温和民主派若支持政改方案，而参选立法会的议员与支联会的核心成员又有所区别，北京有可能放下「六四」包袱，邀请民主派成员到内地，就香港政制发展问题作实质交流，连会面的地点和中方官员的级别都有谈及。按这构想，政改方案通过后，中央高级官员会发表公开讲话，支持香港在2017年和2020年实现双普选，并就迈向普选的路径作一些原则性阐述，确认跟民主派沟通的成果。<br />
<br />
（4）踏入4月，即使亲北京阵营中一些「鹰派」人士不断放风攻击普选联的方案，而工商界和左派亦放软手脚，没有就普选办法提具体方案或反建议，但特区政府高层仍不断劝温和民主派要忍耐，要对沟通保持信心，说很快便会有更高层次的接触、会有更实质的方案，还发誓说仍未数够40票，又说应该不会在5月16日补选前提交修订方案。<br />
<br />
正因上述信息，普选联的核心人士虽然偶有疑心，患得患失，但万万没有料到，政府会在距离5.16补选尚有一个月，就突然公布一个没有显著让步和改进的保守方案。这等于宣告之前寻求对话的努力尽付东流，难怪有普选联核心成员感到被骗。<br />
<br />
政坛耳语续称，最教他们气上心头的是，直至昨晨，特区政府高层以至个别向来跟民主派有倾有讲的行政会议成员，仍然轮流发动电话攻势，四出联络普选联成员，苦口婆心地劝喻他们，在政务司长唐英年公布政改方案内容后，千万不要实时「反」，着他们忍耐一下，先听过人大常委会副秘书长乔晓阳下午四时半的发言后，才「翻脸」也未迟，还煞有介事地说「乔老爷会特别回应你们的」。可惜，听罢乔老的发言后，普选联中人更火上心头，感觉被特区政府「笨」到极点。<br />
<br />
杨森s要继续争取对话<br />
<br />
不过，在一片喝倒采声中，普选联成员杨森昨晚特别来电嘱咐笔者，要帮他记录如下几句话s「（民主派）不要因昨日的方案灰心，因大家的着眼点从来都在2016、2017、2020年的选举安排，而不是2012年，今年七月若方案没有改善，那便否决它，但要坚持争取以理性及据理力争的方法与中央对话，以实现终极普选」。<br />
<br />
李先知<br />
