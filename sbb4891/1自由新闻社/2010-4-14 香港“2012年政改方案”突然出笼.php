													<h2>探针：乔太爷乱点鸳鸯谱</h2>
						
						
																											<div id="postmessage_571246" class="t_msgfont">Apple Daily 苹果日报网上版 －－2010-04-19<br />
探针：乔太爷乱点鸳鸯谱<br />
<br />
乔晓阳谈政改方案纯属宣谕性质，不许香港记者提问，更画地为牢，规定「要一步一步走，五步走完才能进入这个大门。」听去就像宗法祠堂的老太爷训斥重孙子似的。<br />
<br />
共产党极权政体的核心价值，就是对社会的严密控制；党的权力和控制欲，又拒绝任何法理约束──包括它自己的党章和中华人民共和国宪法。与毛时代「和尚打伞，无法无天」相比，后极权主义却讲游戏规则，只不过这规则要由它来定。<br />
<br />
最能揭示游戏奥秘的就是两会，那既是国家礼乐，也是政治仪式。党庄严地敦请两会代表行使权利监督政府，却对代表的言行举止实行监控，仔细地过滤剔除异质的声音；党要求与会代表投下神圣的一票，但代表本身却不是被「神圣的一票」选出来的，而是层层指定的。写到这里，笔者都觉得是陈词滥调，不是我的笔墨言辞，而是歹戏拖棚的两会把戏。<br />
<br />
不过这次两会有段花絮，堪作香港终极普选联盟之参考──两会开幕前夕，十三家报社推出共同社论，呼吁两会代表运用「人民赋予的神圣权力」，去推动户籍制度改革。这非但毫无政治挑战意味，连敏感性也谈不上，因为国家总理温家宝已公开许诺户籍改革。问题就在这里──同样的话由党和政府来宣布，就是伟大光荣正确的决定，是阳光雨露一般的党疼国爱；而由未经许可的媒体共同社论说出来，就成了民间争取，对集权政体构成了某种压力，哪怕这一要求完全「政治正确」，党国也不能答应，遂马上撤销社论，在网络上彻底消音，十三家报社受到警告处分，社论起草人被开除。<br />
<br />
这正是游戏规则的内核，共产党认定香港政改前行的每一步，必须由中央政府恩赐；香港人的民主权利并非天赋，亦非《基本法》所赋予，而是党赐的。莫说中央目下无意给出普选路线图，就算港人乖乖地按照乔晓阳的「五步式」，莲步款款地迈进普选大门，对共产党而言，届时双普选也必须是中央操控的游戏，它要的是一个可以控制结果的选举，党的意志绝对凌驾于社会契约和法定程序之上。<br />
<br />
更须记住，所有政改步骤，都不容香港民间压力团体和中央谈判索讨，乔晓阳这番祠堂训话，足以让任何「谈判」幻想破灭。共产党只在势危之际才会放软身段，如京奥年的藏变，中央屈尊和达赖喇嘛的代表谈判了；如山崩地裂的川震，被禁的地下教会出来救灾了。尽管危机过后一切回复原状，但由公民意志谱写的一段历史，至今未能涂改，就是香港○三年的五十万人大游行。<br />
<br />
