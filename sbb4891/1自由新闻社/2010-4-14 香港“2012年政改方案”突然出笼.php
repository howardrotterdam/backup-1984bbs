													<h2>苹论：为祸三十五年还不够吗？</h2>
						
						
																											<div id="postmessage_571204" class="t_msgfont">Apple Daily 苹果日报网上版 －－2010-04-19<br />
苹论：为祸三十五年还不够吗？<br />
<br />
为了竞逐特首，为了改善自己在市民心目中的形象，向来支持小圈子选举及功能组别选举的行政会议召集人梁振英先生上星期六忽然发炮，批评一些功能组别议员只顾争取业界利益，只顾博取界内选民的支持，忽略向市民问责；并指这些议员有需要检讨。<br />
<br />
梁振英先生从《基本法》草拟年代已积极参与筹划回归后的政制方案，六四镇压后出现的「一会两局」模式及立法会分组点票制度也跟梁先生及他的政治盟友有密切关系。今次忽然转把枪头指向功能组别议员大概只是政治上的权宜之计，未必是他真心诚意的想法。但从这位历来「听听话话」的政客也要公开批评功能组别来看，这种维护少数人特权，妨碍政制向民主迈进的选举方式显然已成了大部份人厌弃的东西，显然已成了该尽快淘汰的过时反动货色，甚至建制派核心人物也想尽量跟它划清界线。<br />
<br />
功能组别在香港已存在了四分一世纪。只要翻看一下这二十五年来的历史特别是立法局及立法会的历史，就不难看到大部份功能组别议员表现不合格，不称职。他们中不少人经常迟到早退，待在会议厅半小时或更少就离席而去，不知所终。有的即使坐在议事厅十年、二十年仍然面目模糊，谁也记不起他们做了些甚么，提出了些甚么问题，参与制订了甚么政策法案。有的则不过是政府的传声筒或应声虫，只懂支持政府的政策或法案，从没有为市民发声。还有不少常把本身行业、界别利益凌驾整体利益，只为了替本身行业、界别争取好处，情愿牺牲一般市民及整体社会的福祉。 <br />
<br />
当然，市民厌弃功能组别，希望把他们尽快取消不仅是因为议员表现欠佳，更因为这样的选举方式违反民主、公平原则，更因为这样的选举方式延续及巩固政治特权。从选民基础看，很多功能组别选民只有几百甚至几十，而且往往集中在行业的老板或高层管理人员手上；其它市民即使在该行业、界别工作多久，关系多密切也没资格投票，只能任由少数人挑选界别的代表。若果跟选民数以十万计的普选相比，这些只有几十、几百选民的功能组别更明显是个「自己友」俱乐部般的货色，把绝大部份人排除在外。这跟普及而平等的民主原则是完全背道而驰的。<br />
<br />
更可怕的是功能组别延续及强化了政治特权。当年设立功能组别是因为政府认为某些行业、某些人对社会有特别重要性或贡献，需要让他们在议会有一定的席位，保障他们的权益。可以说，功能组别肯定了社会上某些人特别是商人、大企业比其它人重要，比其它人高级，可以享有特殊的议席与权利。这种公然确认特权、确认某些人比其它人高级的做法不但荒谬，也不符合社会共融和谐的原则。而且，确立了类似的政治特权后，拥有特权的人固然死抱不放，其它人、行业也将会千方百计挤进去，希望成为特权分子，拥有高人一等的社会政治地位。久而久之，功能组别及它所代表的政治特权变得牢不可破，世代相传；而社会则变得等级、阶层壁垒分明，失去了自由流动的活力。<br />
<br />
从一九八五年开始引入功能组别到现在已二十五个年头。假若到二○二○年才全面取消，功能组别及由它衍生的政治特权已为祸香港三十五年，已污染、毒害香港的政治文化、体制达三十五年之久。还有理由让这样的怪物继续存在下去吗？<br />
