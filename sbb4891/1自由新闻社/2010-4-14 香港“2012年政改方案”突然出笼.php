													<h2>唐英年称「更进步」　泛民直斥其非：「仲烂过 05年　点食得落？」</h2>
						
						
																											<div id="postmessage_565297" class="t_msgfont">Apple Daily 苹果日报网上版－－2010-04-15<br />
--------------------------------------------------------------------------------<br />
唐英年称「更进步」　泛民直斥其非：<br />
「仲烂过 05年　点食得落？」<br />
&nbsp;&nbsp;<br />
<img src="http://pic1984.12hp.de//up/1271341184.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
政务司司长唐英年（中）挂帅推销政改，政制及内地事务局局长林瑞麟（右）在记者会尾段收拾文件准备离开。区民杰摄<br />
<br />
【本报讯】政务司司长唐英年昨日化身「人肉录音机」，强销今次政改方案较 05年遭否决的方案「更进步、更民主」，却遭泛民主派踢爆方案其实较 05年「更烂、更倒退」，特首选委会的民选区议员由 05年的 400人削至仅 75人；政府亦拒绝如 05年般承诺取消区议员委任制，只称方案通过后才提出建议供社会讨论。有泛民成员直斥：「今次个方案仲烂过 05年，叫我O点食得落？」<br />
记者：林俊谦<br />
<br />
唐英年昨日推出自称较 05年「更进步、更民主」的政改方案，惹来泛民成员炮轰。此方案删去 05年将 400个民选区议员加入特首选委会的建议，改为只加入 75名民选区议员，再将属于原政界的余下 25个议席，分别将 10席分予立法会、 10席政协以及 5席乡议局，其余 300席则平均分予选委会其它三大界别。<br />
<br />
有泛民中人分析，今次的方案其实较 05年方案更保守，除了特首选委会的区议员数目大幅减少，削弱了选委会民主成份外，立法会组成方式也是原地踏步，特别在取消区议会委任制的问题，政府根本没有作出实质承诺，只答应通过政改方案后，研究以本地立法方式取消，「若果到时保皇派反对，钫府系咪又缩沙，话社会庸彩叮于是又唔去做？」总括而言，方案只是削弱特首选委会及立法会的民意代表性，较 05年方案更差。<br />
<br />
涂谨申当场撕烂方案<br />
<br />
独立议员何秀兰认为，今次方案将特首选举门坎提高至 150人，又将 300个新增选委会成员，分到泛民难以取得提名的工商、金融、专业等界别，「令下次特首选举，将更难出现代表市民声音候选人，同北京钦点候选人竞争，呢个完全系一个倒退。」她又批评唐英年昨日推销方案时只懂充当「人肉录音机」。<br />
<br />
职工盟李卓人则不满政府至今拒绝交代普选立法会时会否废除功能组别，令人大所谓普选时间表变成伪普选时间表，「今次仲烂过 05年，叫我O点食得落？」民主党涂谨申会议上更因不满方案内容，当场撕烂方案，批评方案民主倒退，「我O仲有乜理性、和平、忍让基础？」<br />
<br />
 <img src="http://pic1984.12hp.de//up/1271341195.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
特首曾荫权昨日发表讲话，呼吁议员通过政改方案。政府新闻处图片<br />
<br />
 <img src="http://pic1984.12hp.de//up/1271341209.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
多位高官对唐英年的讲话不感兴趣，在议会内纷纷闭目养神。成启聪摄<br />
<br />
撑功能组别尽心尽力<br />
 <br />
<img src="http://pic1984.12hp.de//up/1271341292.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
