特首及特区政府没有说错，大部份市民都不希望政制在二○一二年再原地踏步，大部份市民都想看到政制可以在二○一二年向前发展。可是，大部份市民也清楚表明希望政制朝最终普选的目标迈进。拿这个标准来看，特区政府昨天提出的二○一二年政改最终方案是不合格的，是令人失望的，是无法支持的。<br />
<br />
应该看到，大部份市民包括泛民主派政团、议员并没有再要求二○一二双普选，并没有再坚持在后年就普选行政长官及所有六十位立法会议员。大家希望得到的不过是到二○二○年可以确实取消立法会的功能组别议席，以符合《基本法》普选立法会的要求，让香港拥有真普选。可惜，特区政府昨天推出的终极方案对这个卑微、务实的诉求没有丝毫实质的响应，只是不断推搪说政府没有得到授权处理二○一二年后的政改，只是不断重申踏出这一步后海阔天空。这怎不令人失望及反感呢？<br />
<br />
事实上希望到二○二○年取消功能组别议席不是甚么非份的要求，不是甚么超乎法理或常理的要求。只要中央政府及特区政府认同国际人权公约及民主国家有关普选的理念与定义，功能组别这种不符合普及而平等原则的选举制度必然最迟要在二○二○年彻底消失，不复存在。换言之，大部份市民及泛民主派政团没有要求甚么额外承诺，只希望中央政府及特区政府按本子办事，按游戏规则办事，不要三心两意而已。偏偏中央政府及特区政府连公开表示按本子办事、按国际公认民主准则办事的勇气及意愿也没有，反而不断强调功能组别存废问题要待下回分解。这怎不让人怀疑中央政府及特区政府落实普选的诚意呢？<br />
<br />
人大常委会副秘书长兼基本法委员会主任乔晓阳先生昨天再次重申较早前人大提出的普选时间表是算数的，是认真的。通过政改五部曲，包括立法会以三分二大多数通过方案，双普选便能实现。乔先生愿意代表人大及中央政府再次确认普选时间表无疑是件好事，但既然乔先生及中央政府也明白市民需要一个更清晰、更确实的承诺，他理应就普选提出一些补充解释，说明功能组别从原则上不符合普选的定义，不能长期存在。只要乔先生能作这样的补充，让市民知道中央政府对普选没有三心两意，政制的僵局实时可解，政府提出的二○一二年政改方案立时可以视为向全面普选迈进的一步。遗憾的是，乔先生没有作这样的说明，特区政府也没有作这样的承诺。在前景不明的情况下，政府提出的二○一二年政改方案顿时变成目标不明、方向欠奉的苍白文件。像这样的文件，这样的方案，市民怎能接受呢？<br />
<br />
没有人希望二○一二年的政制原地踏步，泛民主派政团及议员不管是争取沟通的或推动公投的都希望政制向前发展，都希望尽快落实真普选。只是，功能组别已成为香港迈向真普选的拦路虎、绊脚石。不移走这块绊脚石，香港政制将会继续「唔汤唔水」，将无法实现普及而平等的选举。为了落实《基本法》最终普选的目标，为了符合市民的期望，泛民主派政团及议员必然要企硬，必然要坚持废除功能组别的！<br />
<br />
周一至周六刊出</div>

							
							
							
							
