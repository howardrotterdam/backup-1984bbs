政府推出鸡肋方案 民主进程命悬一线 <br />
<br />
【明报专讯】泛民阵营的温和派（下称温和派）一直期望与中央沟通，寻求终极解决普选安排之际，特区政府「突然」公布2012年两个选举办法的方案，此举显示中央与温和派沟通的诚意有值得商榷之处；另外，昨天公布的政改方案并未触及阻碍民主进程、弊病丛生的传统功能组别，使人质疑中央和特区政府落实真普选的诚意。<br />
<br />
勿让温和派平白牺牲 <br />
<br />
当权者要有后续善意行动<br />
<br />
这次政改咨询，泛民阵营就温和与激进两条路线出现分裂，情况对泛民阵营和政治生态意义重大。选择激进路线的公民党和社民连，搞5区辞职变相公投，要动员市民对中央和特区政府施压；温和派则不同意所谓辞职公投，选择寻求与中央沟通对话，解决政制的深层次矛盾。这两条路线，并非各有各做那么简单，而是温和派冒着被指为转、投共的巨大压力，希望与中央和政府开展新的互动模式，解决政改问题。<br />
<br />
过去3个多月，温和派所获响应，曾经使人有所憧憬，例如，中联办主任彭清华表示，注意到「在政制发展问题上，理性讨论的声音有所增强」；政务司长唐英年则与终极普选联盟会晤，答允转达希望与中央沟通对话的要求；政制及内地事务局长林瑞麟也破天荒出席民主党的内部会议，讨论政改问题。连串事态发展，使温和派憧憬有可能与中央沟通，社会上不少人也这样理解。残酷的现实是，到特区政府公布政改方案之时，温和派连一次有意义的对话也未获安排。<br />
<br />
沟通对话谈问题，基于立场取态不同，可能会失败，但是若真的有诚意沟通，则过程十分重要，因为可以反映双方是否获得足够尊重、抑或是予人一方有被耍弄的感觉。温和派在高风险下寻求与中央对话，若中央认为不必要，大可在早期就表态杜绝温和派幻想，但是社会人士与温和派都感受到特区政府和中央的「有得倾」态度，现在回看，可能只是特区政府和中央为分化泛民阵营，拉一派、打一派，使「辞职公投」的声势沉寂而已。<br />
<br />
除非特区政府和中央对于温和派有后续善意行动，否则，这次温和派在政治上将陷入进退维谷困境。一时之间，温和派可能要为选择付出一定代价，其支持者也会感到沮丧；不过，即使如此，我们认为特区政府和中央不应该因而高兴，因为温和派肯定不会投奔建制阵营，他们只会被迫采取激进取态，香港政治生态势必进一步激化；另外，特区政府消息暗示温和派支持2012年政改方案，以建立与中央沟通的互信基础，我们认为此情况出现的可能性渺茫，因为若温和派这么做，形同政治自杀。经此一役，民主派和特区政府、中央之间的鸿沟更大、更深、更广。这是香港政局的悲哀，香港整体或许会因为内耗加剧而付出沉重代价。<br />
<br />
关于特区政府拿出来的政改方案，虽然口口声声说增加了民主成分，但是完全不处理传统功能组别的问题，使人质疑香港能否实施真普选，并非无的放矢。<br />
<br />
功能组别要循序渐退<br />
<br />
让香港政制循序渐进<br />
<br />
中央和特区政府一贯告诉港人政制要循序渐进，其中潜台词与功能组别的不民主有关，因此若要使政制循序渐进，功能组别必须循序渐退；另外，功能组别不但是政制深层次矛盾的根源，不少社会深层次矛盾与功能组别也关系密切，例如近期张宇人议员倡议最低工资时薪20元，就触及贫富悬殊问题。所以，解决功能组别问题，也可以使其它许多社会矛盾得以纾缓或化解。<br />
<br />
功能组别之不民主、不公义，已经成为包括推进民主化等改革的负累，虽然连自由党也建议扩大功能组别选民基础，但是特区政府以社会没有共识为由，政改方案对传统功能组别原封不动。这样，让市民怎样相信特区政府推动真普选的政制民主化？其实，功能组别若不逐渐淡出，到2020年才一下子全面改造或撤销，那是大变，不符合循序渐进原则。中央和特区政府在功能组别问题上，其实自相矛盾，难免使人对真普选有疑虑。<br />
<br />
我们并非要求一下子全部开放功能组别的选民基础、或是在2012年就全面废除功能组别，而是认为特区政府要有实质措施，使市民看到功能组别真的在逐步退出政治舞台。<br />
<br />
特区政府以没有共识为借口，让功能组别续享政治特权，只是托词。香港是多元社会，对任何议题不可能得到百分之百共识，只能取得最大公约数；另外，现有功能组别议员和他们所代表的利益，当然不会放弃其政治特权，若政府因为他们的反对而加以维护，那是反映政府没有落实真普选的决心。<br />
<br />
政府应该推动讨论，营造共识，而非躲在「分歧」后面的不作为。设若特区政府提出改造功能组别的方案，例如扩大选民基础等，而功能组别议员反对方案，未能取得三分之二议员通过，这样情况就不一样了，因为那是功能组别暴露其阻碍民主化的真正本质，与市民追求的民主化为敌；现在则是功能组别躲在特区政府背后，政府成为保护功能组别、窒碍香港民主化的元凶。<br />
<br />
政改方案公布之后，泛民阵营温和派虽然有受骗感觉，但是他们反对方案，基本上不在自身遭遇，在于方案保守，又未能取得更具体、确切的真普选承诺。我们认为，除非中央和特区政府不在乎方案被否决，否则到方案提交立法会表决之前，政府起码要在功能组别做工夫，让市民看到功能组别最终都要淡出，或是功能组别的选举真的朝着普及而平等的方向前进。在政治上，讲「你信我喇」不会有用，最实际是「无谓讲嘞，做俾我睇喇」！<br />
