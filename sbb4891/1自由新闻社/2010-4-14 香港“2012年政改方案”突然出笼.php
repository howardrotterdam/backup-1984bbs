社运人士计划包围立会<br />
<br />
周澄昨与大专 2012一众成员到立法会旁听政改方案，在唐英年发言时大叫口号，随即被女保安捉住。周澄奋力挣扎，紧拉旁听席上的扶把，边走边呼叫。<br />
