													<h2>京官放火强推烂方案　泛民点灯苦撑真普选</h2>
						
						
																											<div id="postmessage_565277" class="t_msgfont">Apple Daily 苹果日报网上版－－2010-04-15<br />
--------------------------------------------------------------------------------<br />
京官放火强推烂方案　泛民点灯苦撑真普选<br />
<br />
&nbsp;&nbsp;<img src="http://pic1984.12hp.de//up/1271340990.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
乔晓阳<br />
<br />
【本报讯】特区政府昨日公布 2012年政改方案，面对香港主流民意强烈要求普选路线图，港府却只字不提，全国人大常委会副秘书长乔晓阳更亲自力撑特区政府的「烂方案」，扬言港人得先接受政改方案，再跟从人大常委会定下的程序，才有条件实现普选。有泛民主派立法会议员批评乔晓阳「只许州官放火、不许百姓点灯」，在普选问题上完全以我为主，不许港人争取真普选，泛民不会接受，必定投下反对票，并继续点亮民主灯火，力撑真普选。<br />
记者：莫剑弦<br />
<br />
政务司司长唐英年昨日在立法会公布政改方案，一如外界预期， 2012年特首及立法会产生方式的建议，同样以 05年方案为蓝本，只增加特首选委会人数至 1,200人，以及以民选区议员互选方式，产生新增五个功能界别议席。泛民主派要求政府一并交代普选时间表，并承诺取消功能组别却只字不提。唐英年表示，由于中央未有授权处理 2012年后的选举方式，特区政府整理有关意见后，会交由下届政府处理。<br />
<br />
定五部曲要港人跟从<br />
 <br />
<img src="http://pic1984.12hp.de//up/1271341107.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
有泛民主派立法会议员批评乔晓阳「只许州官放火、不许百姓点灯」，在普选问题上完全以我为主，不许港人争取真普选。<br />
<br />
全国人大常委会副秘书长乔晓阳昨日更在北京亲自会见香港记者约 10分钟，但不许记者提问。乔表明中央政府支持特区政府不提普选路线图的做法。他强调，人大常委会定出 2017年可以普选特首、 2020年可以普选立法会，具有权威性及法律效力，但有关程序必须按人大常委会定下的五部曲行事，即先由行政长官提交报告，再提请人大常委会批准，再经立法会审议，三分二多数议员通过及行政长官同意，最后才可提交人大常委会批准及备案，香港人一定要跟从，否则无法走入普选大门，间接否定特区政府提出普选路线图。<br />
<br />
「这五个步骤是法定的、程序性规定，也是必须要遵从的。打个比方，明确时间表，就好像打开普选的大门，但怎么进这个大门呢？要一步一步的走，五步走完了，你才能进这个大门。」乔晓阳又提醒港人要支持今次政改方案，扬言要珍惜今次机会，寻求共识，通过政改方案。<br />
<br />
不过，泛民主派对政府及乔晓阳未有明确交代普选时间表，并承诺取消功能组别，表明不收货。民主党创党主席李柱铭说，乔晓阳的承诺不足，若中央真的有诚意沟通，应作出更大让步，显示中央有诚意谈判。<br />
<br />
民主党议员李永达认为，乔晓阳的讲话未能释除泛民疑虑，包括 2017年及 2020年普选时间表如何落实，以及 2020年立法会会否全面取消功能组别，他认为，若人大常委会决定取消功能组别，应有鲜明立场，并透过特区政府游说现有议员放弃功能组别议席。 <br />
<br />
方案令立法会更倒退<br />
<br />
立法会议员李卓人更指出，乔晓阳对普选的态度就如「只许州官放火、不许百姓点灯」，在普选问题上完全以我为主，「港人要有普选，就要跟住谝馑甲觯谙氲愣嫉谩梗可是不许港人争取真普选，「你O要乜都唔得，系唔畀普选路线图畀你！」这种态度泛民不会接受，方案提交立法会表决时必定投下反对票。<br />
<br />
立法会全体泛民议员昨日开会后，即召开联合记者会，表明一致投反对票，民主党主席何俊仁批评，政府至今拒绝交代实行普选时会否废除功能组别，以及特首选举候选人门坎高低，令港人难以看到普选终点，「今次方案最多系原地踏步，我睇唔到系向前走；方案个设计，只会令立法会更倒退，令议员更不能代表民意。」<br />
<br />
另一主张谈判路线的民协议员冯检基也炮轰政府借香港社会没有共识，拒绝承诺取消功能组别，令人无法接受，「根本个问题有共识，定系庸彩叮政府都系唔做，点可能叫人务实理性讨论？」他表明民协将不会支持方案。民协成员、社福界议员张国柱亦指，政府方案与社会提出的方案差距极大，难以投票支持。另一名温和泛民成员李国麟亦表明反对方案。<br />
<br />
面对泛民强烈反对，民建联主席谭耀宗承认对通过政改方案不乐观，但他仍呼吁民主派应以「大局为重」，又指政府方案「可以做，都已经做」。<br />
<br />
