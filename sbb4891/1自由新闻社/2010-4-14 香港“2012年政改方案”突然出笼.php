唐英年<br />
<br />
【本报讯】政务司司长唐英年昨日突然知会立法会，今日会就 2012年政改方案发表声明，交代特首及立法会产生方式的最终建议。虽然主流民意明确要求政府交出 2017年、 2020年普选路线图，并承诺取消功能组别，但北京及特区政府置若罔闻，完全拒绝接受，泛民主派对北京及港府如此漠视民意感到极为愤怒，表明一定否决政府极保守的政改「烂方案」。记者：莫剑弦、林俊谦<br />
<br />
唐英年今日将在立法会大会发表声明，交代 2012年政改方案最终建议。据了解，根据方案，特首选举委员会由原来 800人增至 1,200人，组成选委会的四大界别各增 100人，特首候选人提名人数由 100人增至 150人。<br />
<br />
立法会方面，港府建议由 60席增至 70席，地区直选及功能组别各增 5席，功能组别 5席由民选区议员互选产生，连同原有 1席区议会功能议席， 6席区议会功能议席将改以比例代表名单制选出。对于泛民要求取消区议会委任议席，政府只答应在政改方案通过后才着手进行。<br />
<br />
李柱铭：十年后都难见普选<br />
<br />
代表主流泛民政党、民间团体及学者的终极普选联盟提出的所有政改建议，包括要求一人一票选出区议会功能议席、明确交代 2017年、 2020年普选路线图，以及承诺取消功能组别，中央及特区政府完全拒绝接纳，甚至连区议会委任议席也是有条件下才可取消，令不少泛民成员感到十分愤怒，认为北京及特区政府根本无意响应港人任何对普选的诉求，泛民根本不可能通过如此的政改方案。<br />
<br />
民主党创党主席李柱铭形容政府方案令人十分失望，完全没有对真普选作出承诺，「十年后都唔知睇唔睇到普选，」他又指按目前方案内容，看不到民主党可以支持，「我睇O（民主党会员大会）几难通过。」<br />
<br />
余若薇：公投让政府听民意<br />
<br />
民主党副主席刘慧卿表明，不能接受政府拒绝交代普选路线图的方案，「现时（政改方案）搞成睿真系好差，如果系睿我相信民主党系唔会支持」，表明该党不接受政改「烂方案」，「我同你倾，唔代表我O要跪低晒！」<br />
<br />
一直支持与北京沟通的张文光也说：「如果又醒胝府对 2017同 2020年双普选保证，民主党系唔会支持 2012现有呢个方案。」<br />
<br />
公民党党魁余若薇认为，今次政改方案内容反映政府至今未有听到市民心声，她希望市民能在 5月 16日五区公投日，透过手中一票让政府清楚聆听民意。<br />
<br />
有泛民成员透露，泛民主派及终极普选联盟本周内将召开会议，讨论政改立场，初步意见是政府方案太保守，看不到任何泛民议员可以接受，「我睇连民协都涌赡芙邮苣馗龇桨浮！<br />
