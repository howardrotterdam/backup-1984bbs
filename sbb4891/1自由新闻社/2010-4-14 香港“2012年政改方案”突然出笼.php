													<h2>选民要让自己火起来</h2>
						
						
																											<div id="postmessage_562990" class="t_msgfont">Apple Daily 苹果日报网上版 －－2010-04-14<br />
选民要让自己火起来<br />
<br />
尽管特区政府不愿不愿还需做──五一六选举最终也启动起来。<br />
<br />
选举管理委员会所有开支都来自公帑，即是说，五一六这场选举跟特区政府绝对是捆绑式。心生不忿的建制派一直指五一六是「违法违宪」的选举，却又不能具体指出究竟违了哪条法、哪章宪？<br />
<br />
扫走议会的垃圾<br />
<br />
打击五一六手段不遗余力得「急」不择食，有亲建制的学校拒绝借出校址作票站玩抵制，此举却不会难倒参选人，反难为了选管会，须知无论选民多寡，选管会都要保证有足够票站。也有建制派议员要求特区政府仿效烟包上印警告字眼，在寄发的投票通知书印上「香港特区政府忠告全港市民，任何形式的公投都是违宪违法，政府不会承认」字样。不禁要问，五一六选出来的议员身份是否也不被承认呢？幸而律政司司长黄仁龙头脑较健全清醒，以「非负责范围」拒绝这个随时陷特区政府不义之馊主意。更有人索性把这选举聚焦于一亿五千万元公帑，比起特区政府长期养起一班薪高智低的庸官，一亿五千万元公帑造就一个全民表达普选诉求机会，绝对物超所值！<br />
<br />
别有用心者最爱「移形换影」，企图以偷换概念去掩盖五一六选举的真正主题，把矛头直指社民连三子在议事堂的火爆言行「教坏细路」。笔者也不完全认同社民连三子所有火爆言行，但相对那些从立法会出来嬉皮笑脸，向大群市民竖起中指的猥琐佬，以及曾公开嘲讽「十个女人十个系鸡」的贱男人，社民连的恶形恶相只属小巫见大巫，为何一众「正义超人」多年来都不挺身扫走那些「教坏大人」的议会垃圾──因他们都来自功能组别？<br />
<br />
五一六选举只有争取真普选这个单一议题。虽不具法律效力，作一次清晰的全民表决有何不妥？<br />
<br />
争取普选的理念不应受任何外在因素影响，我们不用听阿爷「吹鸡」识做归边；不用听从任何政府庸官、政界权威、党派大佬的明示、暗示引导我们行使投票权。任何政治交易、政党恩怨、面子之争、口舌之战，都不该左右我们投票意向，我们的选票独立于任何政党利益，凌驾任何道德光环，我们目的明确；争取一个本来就属于我们的真普选！<br />
<br />
五一六既是一场合法合宪的选举，行使选民权利也是磊落光明而神圣的事，不要像某些人的投票态度暧昧得犹如艺人婚姻……<br />
<br />
走在前线的公社两党明知不可为而为之背水一战，此战一旦失利对后方的普选联也见不得有好处，到时那些「信差」、「公公」之流都懒得跟你游花园……<br />
<br />
用选票顶住冷待<br />
<br />
五一六是场空前绝后的艰苦工程。面对一个表态争取真普选的重要日子，作为支持民主的选民实在找不到不投票的理由，轻易放弃行使自己的公民权利等如「自废武功」，正中当权者下怀！关键时刻不为自己相信的民主理念表态，还配谈甚么支持普选！<br />
<br />
五一六务必以实际行动捍卫我们的公民权利，用选票顶住一切冷待、冷嘲、冷眼、冷箭；支持真普选的选民先要让自己火起来！<br />
