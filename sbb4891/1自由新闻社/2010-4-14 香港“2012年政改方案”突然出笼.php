《各界力撑》<br />
<br />
大专生--率先向唐英年发炮<br />
<br />
政改烂方案燃点全民怒火，出战立法会新界东补选的「大专 2012」成员周澄率先发炮，昨日在立法会旁听席向准备发言的唐英年大叫：「 2012双普选」、「打倒特权阶级」、「废除功能组别」等口号，随即被保安员带走。有社运人士计划在政改方案表决日包围立法会，重现反高铁画面，动员全港民把烂方案拉下马。<br />
