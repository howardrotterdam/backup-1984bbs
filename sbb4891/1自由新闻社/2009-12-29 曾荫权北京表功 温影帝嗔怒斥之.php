观察家指出，香港政府在这一前英国殖民地进行选举改革的问题上正遭遇反对派方面的挑战。香港立法会中的民主派议员批评政府所计划的选举改革离真正普选的要求还有很大差距。北京中央政府对曾荫权在解决相关问题上表现出来的优柔寡断日益不满。<br />
<br />
选举改革问题给曾荫权对北京的3天述职之行蒙上阴影。温家宝的批评是曾荫权从2005年上任以来所曾受到的最严厉的一次。<br />
<br />
香港电台报道说，温家宝表扬曾荫权应对经济问题颇有成效，但敦促他在解决“有深刻背景的冲突”上应更讲求效果。分析家注意到，胡锦涛数天前在澳门称赞当地政治稳定，有效了实施“维护国家安全法”。在香港，类似的法律却因反对派的抵制而迄今未能通过。温家宝的此番表态被看成是北京对曾荫权未能使香港政界取得一致，并有效掌握反对派的间接批评。（据路透社）</div>

							
							
							
							
