<strong>北京领导人批评曾荫权</strong><br />
<br />
中国主席胡锦涛和政府总理温家宝对正在北京向中央政府作年度述职的香港特首曾荫权提出批评。<br />
<br />
据路透社发自香港的报道，胡锦涛今天晚些时候在会晤曾荫权时敦促香港特区政府妥善处理政制发展问题，维护社会和谐稳定。<br />
