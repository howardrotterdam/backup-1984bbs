温家宝训特首深层次矛盾未解决议员：重提旧话　显示曾班子失败 <br />
<br />
【本报讯】到北京述职的特首曾荫权昨天先后获国家主席胡锦涛及总理温家宝接见。特区政改咨询方案出炉后，胡锦涛首度开腔要求曾荫权妥善处理政制发展问题，温家宝更重提 05年的「训示」，再次要求曾荫权要更好地解决香港的深层次矛盾。有泛民主派认为中央领导人事隔四年后提出相同要求，显然不满曾荫权的表现；有港区人大代表指曾班子解决矛盾问题上「慢半拍」。记者：许伟贤、林俊谦<br />
<br />
曾荫权昨日早上先到中南海紫光阁，向温家宝汇报特区最新发展。温家宝在开场白中赞扬特区政府成功带领特区走出金融危机，接着一番话则概括要求曾荫权关注经济、深层次矛盾及民生问题；包括嘱咐特区继续保持经济健康发展，又提醒曾荫权同时着手研究涉及全局和宏观的重大问题，「统筹全局，谋划未来，更好地解决香港的一些深层次的矛盾。」温家宝重申中央会一如以往给予特区大力支持，但亦提醒曾荫权关注民生，「（要）更加重视发展社会事业和关注民生，以增进港人对未来的希望」。<br />
<br />
死撑矛盾在经济发展 <br />
<br />
今次已是温家宝第二次公开要求曾荫权解决香港深层次矛盾。 05年接任特首的曾荫权首次上京述职时，温家宝当面指香港仍有一些深层次的矛盾未解决，当时政改方案也是刚出炉不久，似乎意在指向政改问题。曾荫权昨天在记者会上「死撑」，指会面时主要汇报特区经济发展，认为温家宝所指的矛盾只涉及本港长远经济发展问题。但不少政界中人认为温家宝所指的矛盾不止经济范畴。据了解，中央一直关注特区凡事政治化情况，认为社会因而变得躁动不安；近期的高铁拨款问题，中央也留意到已经变得政治化，相信温家宝要求曾荫权解决的深层次矛盾就是社会「泛政治化」的问题。接近中方消息人士指，中央一向认为搞好经济与民生，民怨便会随之减少，令社会得以稳定发展，因此才大力叮嘱曾荫权搞好民生。民主党主席何俊仁则认为温家宝冲着政制问题开腔，因曾荫权上任以来仍未能平息政改争议，为特区管治班子带来压力，难怪要领导人再出口教训。对于温家宝促曾荫权搞好民生，何俊仁指，若中央认为透过改善经济民生，市民便会降低政治诉求，这种想法是本末倒置，「一切贫穷不公同利益输送问题，都系源于政治上矛盾，呢一点中央点解唔明？」<br />
