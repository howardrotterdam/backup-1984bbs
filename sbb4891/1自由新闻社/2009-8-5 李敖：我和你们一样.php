　　第一次官司没有坐牢，1967年，时年32岁，案由是妨害公务。事件因14年前的一桩旧案引起。当时李敖18岁，正念高中三年级，他的老师地下共产党员严侨被捕。李敖因师生之谊对师母和三个小孩多有关照。到了1966年，李敖发表《历史与人象》《教育与脸谱》《上下古今谈》《乌鸦又叫了》《孙悟空和我》等书，为当权者所厌恶。于是旧事重提，把他1953年与共地下党员严侨老师合谋偷渡大陆一事翻出来，并于1967年4月8日提起公诉。此事后被判刑1年，但由他的恩师多方奔走，没能入监执行。这大概就是有的文章说他入狱2次，有的说3次的原因吧。实际上这次并没有“入狱”。<br />
<br />
　　第二次，1971年3月19日晚上被捕，时年36岁。案由是台独。事件的起因是被称为台独教父的彭明敏偷渡外逃。当时台独势力还处于萌发阶段。参与彭明敏台独活动的主要干将是他的两个学生谢聪敏和魏廷朝。李敖与他们过从甚密，参与了他们的一些活动，以至被他们作为“外省人也参加台独”的实证到处宣扬。当时蒋介石还在世，对台独活动采取严厉镇压政策，彭、谢、魏等人东躲西藏，李敖给予一定资助。彭终于1970年1月当局抓捕之前偷渡出境，随后谢、魏被捕。他们在审讯中供出了李敖。情报部门经过了长期的软禁、跟踪，最后将李逮捕，并于1972年2月28日以叛乱罪判刑10年，褫夺公权6年。李敖没有提出上诉。1975年蒋介石去世，台湾实行大赦，李敖获减刑，于9月22日改判8年6个月。1976年11月19日出狱，时年41岁。实际坐狱时间为5年8个月。<br />
<br />
　　第三次，1981年，时年46岁，案由是背信和侵占。事件的起因还得从《文星》杂志说起。这是由原中央通讯社社长萧同兹之子萧孟能于1957年创办的一份自由主义月刊。1961年26岁的李敖从国军退伍，萧大胆启用，委以重任。李敖以《文星》为阵地发表《老年人和棒子》、《传统下的独白》等有影响的作品，从而声名鹊起。1965年《文星》杂志被查禁，文星书店被迫关闭，债权人盈门，萧不得不逃往岛外暂避。行前他将超过2000万元的家产托付最信任的李敖代管，不想半年后萧孟能回国后，李敖却将所托之财产悉数据为己有，以至萧不得不与之对簿公堂。经两审，李敖侵占罪名成立，终审判决入狱6个月。1981年9月10日入狱，1982年3月10日刑满出狱。<br />
<br />
　　由此可以看出，事实是：李敖三次被控，两次入狱，坐狱时间总共累计6年另两个月。两次入狱的案由分别是台独和侵占他人财产。所谓三次入狱，累时20年，因言获罪，叛国罪，颠覆国家罪，等等，纯系子虚乌有。至于传播“自由、民主、开明、进步”等观念就根本就扯不上干系。<br />
<br />
说到李敖的言论，虽然尖酸刻薄辛辣大胆敢于指着两蒋父子和台湾当局的鼻子骂，不过事实上，国民党并没有以他的言论治罪。李敖被控罪的事实都不是捏造出来的。<br />
<br />
　　就以“台独”罪名而论，我们且把在警总保安处处长吴彰炯少将主持下审讯中李敖的供词，同案谢聪敏和魏廷朝的证言，军法处检察官的诉状全都 弃之不顾，只看李敖出狱后多年自己写的《年表》，就可以知道那个罪名对他并不冤枉。让我们看看其中的几个片断：<br />
　　“1969三十四岁。以贩卖旧电器维生，暗中支援其他出版活动。义助彭明敏。”<br />
　　“1970三十五岁。一月，因彭明敏偷渡，全年被警总软禁、跟踪。九月三日，在被跟踪中约集外国记者，接雷震出狱。”<br />
　　“1971三十六岁。软禁、跟踪至三月十九日晚上被捕。”“当时谢聪敏、魏廷朝已被捕去一二十天，我因误信两人（尤其是魏廷朝）平时给我的英雄形象，竟以为他们什么也没供出，因此就我所知，一路掩护，什么答案都不吐实，并且不断扯谎以为掩饰。由于我生怕我的口供不利于谢聪敏和魏廷朝两人，结果一开始就给特务们‘李敖不合作’的坏印象，”“魏廷朝说‘两个半’是指他是一个‘台独’，谢聪敏是一个‘台独’，你李敖是半个‘台独’。在台湾肯定的‘台独’，只有你们‘两个半’。事实上，魏廷朝从来没跟我提过什么‘两个半’，我又从何答起？最后侦讯人员告诉我，他们得到的情报是以某某（指彭明敏──引者注）为首的叛乱活动‘台湾本部’有五个委员，我是其中之一。我听了大惑不解，因为关于这‘台湾本部’，事实上，我一无所知，乃是被逮捕以后才得知的。听了这一天方夜谭，我说半个正好是‘两个半’的五分之一，‘两个半’自是指五委员而言，这样看来，‘两个半’岂不正是五委员的秘密代号吗？──我在忧患中仍不失幽默，这段讽刺，我记忆犹新。”<br />
<br />
　　“两个半”演绎成五个委员，确实有点滑稽。不过从李敖自己在没有压力下自己写的这段文字看，说李敖是‘台独运动’的“启蒙者“和“创建人”之一，应当不算太过分的恭维。现在被同仇敌忾指为台独的李登辉陈水扁都不过是李敖的“后来人”而已。现在李敖高调反台独，又是一个转弯子快的例证。<br />
<br />
　　至于他第二次入狱的原因，令“大师”感到难堪，我们当然表示理解。<br />
<br />
&nbsp; &nbsp;李敖1959年台大历史系毕业，但是他并没有打算老老实实地做学问。两个月后入伍，以图在军中发展。那个时候“八.二三”炮战还没完全结束，大批军官得到飞速提升。入伍不失为人生的一条捷径。岂料澎湖军营两年过去了，才提了个少尉，照这个速度要提到少将不是得等到猴年马月？于是投戎从笔，开始著书立说。可惜市场反映并不如期，加上当局查禁，并未赚到几个现银。<br />
<br />
&nbsp; &nbsp;于是把李的主意打在朋友的财产上，借萧孟能出走之机一举吞掉托付的2000万家财。在审理这个案子的时候，他的前妻胡茵梦毅然出庭证明李敖将萧孟能的房子、古董、字画等据为己有，仅她记得的就有傅抱石、吴昌硕、齐白石的画，还有扇骨、青花瓷器，以及她印象最深的“红匣子泛黄卷上”的干隆御批。事后胡茵梦更在其自传《死亡与童女之舞》中说，李敖“他的心中只有钱，为了钱他可利用任何不法手段赚钱”。他可以“每天站在窗前用望远镜观察对面一个大厦的工程，想找出施工差错，预备将来以此威胁建设公司送他一栋房子。”胡氏的话李敖在北大清华复旦的讲话里给予了印证，在号称“文化”之旅的这三次演讲中他没有一次不谈到钱。“口袋里没钱，脊梁骨不硬”，抛弃良知、唯利是图，这就是他的哲学。这就是“大师”一向不愿提起多次入狱的经历的真实原因 <br />
