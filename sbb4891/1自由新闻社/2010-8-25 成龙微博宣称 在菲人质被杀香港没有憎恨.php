													<h2>Jackie Chan: Don't worry, HK people don't hate</h2>
						
						
																											<div id="postmessage_797484" class="t_msgfont"><a href="http://rp1.abs-cbnnews.com/entertainment/08/25/10/jackie-chan-dont-worry-hk-people-dont-hate" target="_blank">http://rp1.abs-cbnnews.com/enter ... hk-people-dont-hate</a><br />
<br />
abs-cbnNEWS.com<br />
Posted at 08/25/2010 2:49 PM | Updated as of 08/25/2010 3:48 PM<br />
 <br />
MANILA, Philippines C Amid rising anger in Hong Kong over the bloody ending to a hostage crisis in Manila, action star Jackie Chan called on the peoples of the two territories to be “united.&quot; He also gave assurances that the people of Hong Kong &quot;do not hate.&quot;<br />
<br />
In his Twitter account,&nbsp;&nbsp;Chan wrote that he was saddened to hear about the hostage drama in Manila that resulted in the death of 8 Hong Kong tourists. Hostage-taker Rolando Mendoza, a former police officer, was also killed in the 11-hour standoff.<br />
<br />
Chan had just arrived in Hong Kong from South Korea when he heard the “terrible news” in the Philippines and tweeted his thoughts.<br />
<br />
“Remember the day before in korea we talked about how people should love each other. We already have so many natural disasters..typhoon, tsunami, everything. Humans should be united and not kill or hate each other,” he tweeted.<br />
<br />
He made this call as the Chinese government demanded answers from the Philippine government over the lapses it made in handling the hostage crisis. Hong Kong residents have also denounced the Philippine police as incompetent.<br />
<br />
“If they killed the guy sooner, they will say why not negotiate first? If they negotiate first, they ask why not kill the guy sooner? So sad,” he noted.<br />
<br />
“A lot of things don’t happen to your own country you don't pay attention…. this kind of things always happen around the world. It happened to hk's people, the whole hk is talking about it. Its really sad,” he added.<br />
<br />
But he assured that Hong Kong will not retaliate because of the incident.<br />
<br />
He said: “HK is a nation built by a lot of different people..don't worry! We do not hate!”<br />
<br />
Presidential spokesman Edwin Lacierda, meanwhile, thanked Chan for “extending his hand of friendship&quot; to Filipinos.<br />
<br />
“Thank you for extending [your] hand of friendship. We hope our friends there will extend their hand of friendship to us.... it’s an unfortunate event,” Lacierda said.<br />
