													<h2>中国男人在非洲留下大量黑色私生子</h2>
						
						
																											<div id="postmessage_209176" class="t_msgfont">【瑞士《星期日新苏黎世报》8月16日报道】题：中国综合征――中非混血儿的悲惨命运(记者&nbsp;&nbsp;泽格・米歇尔)<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 维维安・卡隆加对自己国家出现大批中国人从未感到特别兴奋。但她承认，黄种人是善意的，而且拥有无可争议的巨大干劲。这或许不会给赞比亚带来害处，因为这个非洲国家的1100万人口人均寿命仅为40多岁，而且其中一半人失业。无论如何，维维安在2004年7月13日之前是这样认为的。那一天她生下了第二个孩子。让所有人吃惊的是，这个孩子长着与当地人不同的眯缝眼。<br />
<br />
&nbsp; &nbsp; “我原本不该去基特韦的中国餐馆工作，”维维安痛苦地说。基特韦是赞比亚首都卢萨卡西北部的铜带省最重要的城市，那里有十多家中国餐馆、一家卡拉OK厅和一家赌场，经常光顾这里的是该地区铜矿厂的中国工人。在小约纳坦出生后，那家餐馆的老板就消失了，除了一台破旧的中国电视外什么也没留下。<br />
<br />
&nbsp; &nbsp; 此事让维维安的丈夫受到侮辱，他拒绝让这个孩子住到家里。卢萨卡的中国大使馆也没有答复维维安的急切请求。因此，约纳坦今天住在一个贫困的偏远村庄里的外祖父母家中。由于被蛟子叮咬，孩子得了疟疾。<br />
<br />
&nbsp; &nbsp; 另一位名叫贝塔・穆兰加的妇女也亲身体会到，中国人的名声发生了巨大变化。她是一个漂亮的赞比亚少女，2001年她当选谦比西镇小姐。谦比西附近有一座中国公司经营的铜矿。贝塔当选谦比西镇小姐的那年正好18岁。这一称号为她赢得了一位特别的朋友。坐在观众席中一位王(音)先生向她祝贺，还请她喝啤酒。然后他又向她展示了几招功夫，并问她是否然意当他的女朋友。她同意了。<br />
<br />
&nbsp; &nbsp; “那时，我的朋友朱迪丝也和一个中国人出去玩，”她说，“没出什么问题。”因为这些从亚洲新来的人享有良好声誉：在中国人买下之前，谦比西铜矿已经停产多年。中国人投资了数百万美元，并许诺要让这个地区成为黑非洲的经济发动机。<br />
<br />
&nbsp; &nbsp; 在第一年里，贝塔和王先生只维持着柏拉图式的朋友关系。王先生给她钱买衣服，还给她买了一部手机。<br />
<br />
&nbsp; &nbsp; 一天晚上，他邀请贝塔来到谦比西镇边上的中国工人营地。数百名刚从中国来的工人住在那里。贝塔无聊极了：王先生的同事说不上三个英语单词，而且目光几乎从不离开牌桌。<br />
<br />
&nbsp; &nbsp; 他却对自己征服贝塔显得很自豪。他送她回家，并在那里过了夜。一切正常――直到贝塔告诉王先生，她怀孕了。第二天他的电话就再也无法接通。贝塔在中国工人营地的大门口得知，她的情人回北京了。<br />
<br />
&nbsp; &nbsp; 贝塔的父母以前都曾在谦比西铜矿工作，当他们听贝塔说自己意外怀孕时，都沉默不语。但是在听了牧师的建议后，他们说这个孩子是上帝的恩赐。因此这个小女孩被取名“塔万达”，在当地语言中的意思为“上帝的恩赐”。<br />
<br />
&nbsp; &nbsp; 贝塔很少带女儿上街。工人们在她们背后指手画脚，特别还因为又有一个姓常(音)的中国男人每天都去找她。这种情况持续了一年，后来这个人也在一夜之间消失了。2005年秋天，贝塔生下了第二个孩子，长得也是一幅亚洲人面孔，这次贝塔和新生儿被父母赶出了家门。<br />
<br />
&nbsp; &nbsp; 贝塔绝望地向中国营地负责人求助。这个人说，孩子的父亲不可能是中国人，因为他的员工都做了结扎手术。说完他就使劲把门关上了。<br />
