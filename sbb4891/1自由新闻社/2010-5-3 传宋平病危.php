&nbsp; &nbsp; 担任周恩来政治秘书<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 1936年，宋平参加中华民族解放先锋队。翌年加入中国共产党。宋平担任过延安马列学院组织科长，重庆《新华日报》编辑部秘书长，后成为新华社重庆总分社负责人。在国共和谈期间，宋平一直担任周恩来的政治秘书。和谈失败后，奉调回到中共控制区，出任中共哈尔滨市顾乡区委副书记。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 建国后开始在西北工作，任甘肃省委书记。1960年，宋平调任中共西北局委员、兼西北局计委主任，开始在西北地区工作。文革中，宋平曾受到非难。在&quot;牛棚&quot;里关了一年多。后来，陕西要搞生产，成立&quot;生产指挥部&quot;，让宋平当顾问。这样，他才算又开始工作。1972年，出任中共甘肃省委书记、省革命委员会副主任；1977年6月，升任中共甘肃省委第一书记、省革委会主任，兼兰州军区第二政委。是最早反对“两个凡是”的人之一。1992年，胡锦涛进入中央政治局常委会，媒体披露他是被宋平同志发现赏识，调到团中央工作、而后受到邓小平等老一代革命家重视，着力加以培养的年轻干部。1974年，胡锦涛调任甘肃建设委员会秘书，受到时任甘肃建设委员会主任的宋平赏识，此后历任甘肃省建委设计管理处副处长、甘肃省建委副主任。此外，宋平同志任甘肃省委书记期间，温家宝是甘肃地质勘探局局长<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 1989年，75岁的宋平，和江泽民、李瑞环一起，在中共十三届四中全会上增选为中央政治局常委。1990年兼任中国计划生育协会会长，1991 年向党中央建议每年全国人大、政协会议期间召开人口资源工作座谈会，得到中央批准。1992年9月届满后，宋平离职休养。其妻陈舜瑶，曾任清华党委副书记。1917年生于山东济南。早年入清华大学学习，后加入中国共产党。建国后，历任清华大学党委副书记、中共中央西北局宣传部副部长、中央书记处研究室室务委员、顾问等职。<br />
