																											<div id="postmessage_597979" class="t_msgfont">5月4日约晚上九点多，不少推特网友发现，互联网知名企业腾讯提前上线“宋平逝世并发动网友悼念”的信息，随后约两个小时后，可能有关部门下令，腾讯迅速撤销该页面，并没有留下任何删除的痕迹，网友留言也一一不见了（<a href="http://news.qq.com/zt2010/songping/" target="_blank">http://news.qq.com/zt2010/songping/</a>）。推特网友把腾讯镜像贴出，<a href="http://126.fm/43NeSv" target="_blank">http://126.fm/43NeSv</a>。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 宋平是1989年六四后与江泽民一起上台成为政治局常委的，宋平是胡锦涛在甘肃的“伯乐”。宋平并不属于胡耀邦、赵紫阳党内开明派、自由派，宋平与左派陈云较为接近。宋平曾任甘肃省委书记、国家计委主任和中组部部长、政治局委员，有消息称温家宝的提拔也得益于宋平。宋平还是周恩来的政治秘书。<br />
