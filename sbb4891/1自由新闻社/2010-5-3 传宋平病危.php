在参加兰州大学榆中校区“君政基金”管理委员会第四次会议和西北师范大学百年校庆时，宋平指出，无论是发展经济还是其他社会事业都离不开人才。培养人才的关键是发展教育，甘肃要在抓好教育培养自己人才的同时，广开渠道，制定优惠政策，吸引更多的人才参与到甘肃的各项建设中来。<br />
<br />
在兰州高新技术开发区考察时，宋平对亚盛耐特公司的节水滴灌生产技术给予了肯定。<br />
<br />
他说，西北大部分地区干旱少雨，节水对经济和社会的发展非常重要，甘肃要在教育人们养成节约用水习惯的同时，注重发挥科技在节水中的重要作用，进一步做好节水工作。当他得知甘肃联创科技股份有限公司与印度合作建立软件教育学院时表示，发展高新产业没有人才不行，在西部大开发的背景下，甘肃一定要注意培养高新技术人才，大力发展高科技，将科技转化为生产力，更好地为经济建设服务。<br />
<br />
在与省委党校领导和部分地县干部学员进行座谈时，宋平指出，同全国有些地区相比，甘肃的经济较为落后，但干部的思想认识和观念不能落后，特别是对新形势和社会实践中出现的一些现实问题的认识不能落后，要跟上实践的需要。理论研究者更要有超前意识，要着力研究当前面临的重大问题，如加快国有企业改造、解决社会就业、反腐败及转变党员干部作风等。在研究理论问题的过程中，要注重与实践的结合，实践是第一位的，实践中包含着理论，理论反过来指导实践。他说，解决当前面临的重大问题，党委、政府要多想办法，多做工作，从事理论研究的同志也要有紧迫感，要在理论方面提出一些新的见解。党员干部要转变作风，深入实际，深入群众，发挥模范带头作用，更好地发动群众，依靠群众的力量去解决一些实际问题。<br />
<br />
考察中，宋平还希望甘肃省继续加大基础设施、生态和城市建设力度，动员各方力量搞好工作，为群众创造更好的生活环境。（凤凰网历史频道基于传递新闻价值之必要，特补发甘肃日报2002年10月18日标题为《宋平在甘肃考察》文章，以飨读者。）</div>

							
							
							
							
