22日上午纽约联合国总部举行气候变化高峰会议，中国国家主席Hu Jintao在峰会上发言，并参加了分组讨论。但会场外聚集了包括流亡藏人在内的多个海外团体的示威者，抗议中国国家主席Hu Jintao。<br />
<br />
<img src="http://i35.tinypic.com/jkvgu9.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
流亡藏人团体在纽约联合国总部抗议Hu Jintao(2009年9月22日)<br />
(RFI/Chine)<br />
<br />
联合国会场外，聚集了各路示威人群。流亡海外的西藏人团体规模较大，他们举着雪山狮子旗，以一幅巨大的标语点明了抗议的主题，上面用英文写着：“Hu Jintao，失败的领袖、失败的政策、失败的党，西藏自由！”示威者在现场高呼口号：“ 我们要什么？自由西藏！”<br />
<br />
大约100名FLG修炼者在一幅“停止迫害FLG”的大标语后以静坐练功方式抗议。一位姓李的FLG学员表示，FLG提倡通过练功做好人，他们前来是为了跟大家分享，更关键的是抗议当局的迫害。他说：“现在中国大陆还在发生着迫害FLG修炼者的现象。他们虽然没有在表面上，但是他们更隐蔽，手段更狡猾一些，所以一定要立即停止在中国对FLG的迫害。”<br />
<br />
17年来台湾政府今年首次没有通过友邦在联合国总务委员会提出台湾参与联合国的要求，但是仍有身穿“我爱台湾”绿色T恤的人士在联合国前参加抗议活动。台湾加入联合国行动委员会召集人赖洪典表示，台湾目前面临着一个最大的威胁。<br />
<br />
赖洪典说：“同一个时间我们也是向国际媒体、向国际社会表示，我们要保持台湾自由，因为台湾目前最大的一个威胁就是中国共产党，我们不要共产党，我们需要民主，我们继续民主社会！”<br />
<br />
另外还有一些身穿“吃素环保球地球”T恤的环保人士，趁此机会前来宣传他们的环保理念。其中一位名叫陈樱莺的女士说，根据联合国的报告，畜牧业产生的甲烷对温室效应的影响比工业产生的影响要高百分之80，因此她呼吁人们要少吃肉类，以减缓地球变暖的速度。她说：“依照联合国的报告大家应该要减少吃肉，把畜牧业减少，如果能够鼓励有机农场的话，它能制造氧气，减少二氧化碳，可以把地球降温。”<br />
