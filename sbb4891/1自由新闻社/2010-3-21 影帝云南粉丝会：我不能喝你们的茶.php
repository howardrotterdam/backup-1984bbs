													<h2>影帝云南粉丝会：我不能喝你们的茶</h2>
						
						
																											<div id="postmessage_525281" class="t_msgfont">温家宝在云南灾区婉拒村民倒茶水 称运水不容易<br />
 2010年03月21日15:36&nbsp;&nbsp;中国政府网 <br />
　　“我们一定能够战胜这场旱灾”<br />
<br />
　　――温家宝到云南指导抗旱救灾工作纪实<br />
<br />
　　去年入秋以来，我国西南地区遭遇历史罕见的特大旱灾，给群众生产生活造成严重影响。正值抗旱救灾的关键时刻，3月19日至21日，中共中央政治局常委、国务院总理温家宝来到云南省曲靖市，深入旱灾最严重的地区，看望慰问受灾群众，指导抗旱救灾工作。<br />
<br />
　　温家宝指出，目前全国旱情还在发展，北方地区和东部地区又出现低温寡照和雨雪冻害，农业生产面临的形势不容乐观。必须从经济社会发展全局的高度来重视农业，切实做好当前的抗旱救灾工作，抓好春耕生产，千方百计保证今年农业有个好收成，保持经济平稳较快发展和物价总水平基本稳定。<br />
<br />
　　19日下午，温家宝来到陆良县芳华镇狮子口村万亩大麦示范田，实地察看农作物受灾情况。温家宝走进麦田，蹲下身子，拔起一颗大麦察看。由于持续8个月的干旱，大麦只长了不到一尺高，麦穗干瘪。他又抓起地里的土块，用手掰了掰，基本没有水分。温家宝眉头紧锁，神情凝重。当地负责人告诉总理，曲靖数百万亩农作物因旱绝收，正准备水田改旱田，异地育秧，种玉米、马铃薯，争取小春损失大春补。温家宝点点头。他叮嘱当地干部，干旱还可能持续下去，要做好最坏的准备，尽最大的努力，把群众生活安排好，把春耕尤其是大春生产安排好。<br />
<br />
　　陆良县德格海子水库库容160万立方米，由于持续干旱已干涸见底。20日下午，温家宝来到这里，只见库底泥土裂开了一条条纵横交错的缝隙，密密麻麻的泥块像梅花桩一样，让人触目惊心。温家宝踩着干裂的土块走了100多米，弯腰捡起一个蚌壳，沉默良久。当地干部告诉总理，全省目前有580多条中小河流断流，310多个小水库和3600多个小坝塘干涸。<br />
<br />
　　温家宝十分关心灾区人畜饮水保障情况。19日下午，他走进芳华镇蔡官营村。许多村民和预备役民兵正在路旁挖沟，准备安装引水管道。温家宝在沟边蹲下，向村民了解抗旱救灾和生产生活情况，鼓励大家要想方设法打水井，修渠道。村子里，消防官兵送来了两车水，村民正在排队接水。温家宝走过去，向消防战士表示亲切慰问，并询问供水情况。群众告诉总理，政府为老百姓送水，每人每天供应25公斤水。温家宝叮嘱村民，这些水来之不易，要节约着用。他对围拢过来的村民说，今年，西南地区特别是云南遭受了百年一遇的大旱。我相信，只要我们团结一心，各级干部群众共同努力，就一定能战胜这场旱灾。<br />
<br />
　　20日上午，温家宝来到师宗县葵山镇海子村。今年1月，在水务部门指导下，村里打了一口40多米深的井，日出水量达到72立方米。许多村民正赶着牛车排队拉水。听说村民要等两个小时才能接到水，温家宝叮嘱当地干部要尽快查清地下水储藏情况，“再打一口井，大家就不用等这么长时间了。”山坡上，人们正用水泥修一个储水池，温家宝走过去，鼓励施工人员加快进度，争取早日投入使用。他对乡亲们说：“我相信，老天难不倒人，困难总会过去。”温家宝走进村民李老四低矮破旧的土坯房，询问生活情况。他叮嘱当地干部，遇到灾害，要特别关心五保户、低保户和其他困难群众。在陆良县大莫古镇戈依村，地勘部门正在施工打井。温家宝鼓励大家要依靠科学技术，多打井，为群众排忧解难。水利、农业和国土等部门要协同作战，寻找新水源。<br />
<br />
　　20日上午，温家宝来到师宗县葵山镇大麦地村。74岁的村民王顺生正在水窖打水，温家宝走过去，拎起绳子也打上来一桶，看到水很清，他满意地点点头。在王顺生家的院子里，温家宝和村民们围坐在一起，聊起抗旱救灾和春耕备耕情况。王顺生老伴起身，想进屋倒茶水，温家宝微笑着婉拒：“我们不能喝你的水，运来一点水不容易。”看到村民们饮水安全有保障、口粮充足、春耕用的种子化肥也都准备就绪，精神状态不错，温家宝十分欣慰。他问大家还有什么困难？村民王存生说，要是增加一些水利设施就更好了。“你说到点子上了。”温家宝肯定地说，“政府要加大支持力度，不仅解决当前困难，还要从长远解决问题。”<br />
<br />
　　召夸中低产田改造示范区是陆良县现代烟草农业建设基地之一，目前已整理土地2万多亩。20日上午，温家宝来到这里，登上高处，俯瞰四周，只见成千上万亩红土地连成一片，一排大型拖拉机正在平整土地。当地负责人告诉总理，通过坡改梯、零改整，这里田成方、沟成网、路相连、渠相通，土地质量提高了，耕地面积也增加了，“今后可以做到旱能浇、涝能排，旱涝无虞。”看着眼前壮观的场景，温家宝欣慰地笑了。<br />
<br />
　　20日下午，陆良县德格海子水库旁的庄稼地里，村民们在技术人员指导下，正用营养袋和肥床两种方式培育玉米苗，以便气候条件具备时随时移栽。温家宝认真观看育苗过程，询问有关情况，还在草垫子上坐下来，往一个个塑料袋里添加营养土。他称赞大家为抗旱想了这么多办法。他说，面对旱灾，政府的支持固然重要，但是群众的努力更加重要。<br />
<br />
