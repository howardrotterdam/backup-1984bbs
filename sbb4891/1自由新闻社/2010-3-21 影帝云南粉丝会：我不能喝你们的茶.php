													<h2>西南旱情告急影帝愁眉紧锁 指示做最坏准备</h2>
						
						
																											<div id="postmessage_526478" class="t_msgfont">　西南旱情告急 温家宝愁眉紧锁<br />
<br />
　　据新华社电 去年入秋以来，我国西南地区遭遇历史罕见的特大旱灾，给群众生产生活造成严重影响。目前，云南、广西、贵州、四川、重庆五省区市旱情加剧。中央气象台继续发布干旱黄色预警，未来3天西南无降水。<br />
<br />
　　3月19日至21日，中共中央政治局常委、国务院总理温家宝来到云南省曲靖市，深入旱灾最严重的地区，看望慰问受灾群众，指导抗旱救灾工作，提出4点抗旱救灾措施，并要求从长计议，大力加强水利设施建设，从根本上增强抗御自然灾害的能力。<br />
<br />
　　要做好最坏的准备<br />
<br />
　　据统计，云南等西南五省区市干旱受灾人口达到5000多万人，1600万人饮水困难。旱情对农业生产造成的影响已向上游市场传导。<br />
<br />
　　2010年3月19日下午，温家宝来到陆良县芳华镇狮子口村万亩大麦示范田，实地察看农作物受灾情况。温家宝走进麦田，蹲下身子，拔起一棵大麦察看。由于持续8个月的干旱，大麦只长了不到一尺高，麦穗干瘪。他又抓起地里的土块，用手掰了掰，基本没有水分。温家宝眉头紧锁，神情凝重，他叮嘱当地干部，干旱还可能持续下去，要做好最坏的准备，尽最大的努力，把群众生活安排好，把春耕尤其是大春生产安排好。<br />
<br />
　　2010年3月 19日下午，温家宝在芳华镇蔡官营村察看情况时，对村民说，今年，西南地区特别是云南遭受了百年一遇的大旱。我相信，只要我们团结一心，各级干部群众共同努力，就一定能战胜这场旱灾。<br />
<br />
　　优先解决群众饮水问题<br />
<br />
　　2010年3月20日上午，温家宝来到师宗县葵山镇大麦地村村民王顺生家，王顺生老伴起身，想进屋倒茶水，温家宝微笑着婉拒：“我们不能喝你的水，运来一点水不容易。”<br />
<br />
　　2010年3月19日和20日晚上，温家宝分别主持召开基层抗旱救灾工作情况汇报会和座谈会。他指出，抓好抗旱救灾工作，要全面落实4项措施：一是优先解决群众饮水问题。二是扎实抓好春耕备耕。力争做到小春损失大春补，粮食损失经济作物补，种植业损失养殖业补，农业损失非农业补，努力夺取今年农业丰收。三是严密防范森林火灾。加强火灾隐患排查，严密监测火情，完善应对预案，努力避免发生重特大森林火灾和重大人员伤亡。四是保持灾区市场和社会稳定。要保障粮油、肉类、蔬菜等的市场供应，搞好重要商品物资的运输调度，加强市场监测和调控，防止价格大幅波动。<br />
<br />
　　贵州：需要口粮救济<br />
<br />
　　根据贵州省政府通报的最新旱情，目前该省因干旱灾害有310多万人需要口粮救济。截至18日，贵州省有84个县(市、区)不同程度受灾，受灾总人口1728万人，有557万人、267万头大牲畜饮水困难；需要口粮救济的人口为312.9万人，因灾造成直接经济损失28.77亿元。<br />
<br />
　　四川：人工降雨也没用<br />
<br />
　　随着旱情的加剧，四川攀西地区70万人生活用水出现困难。往日的良田在烈日的晒烤下早已“伤痕累累”，区域内溪流河沟也大多断流。按照四川省“十年九旱”的经验，人工增雨是及时缓解旱情的有效办法之一。而自去年入冬遭旱以来，人工增雨这一良策在攀西地区就丧失了作用。</div>

							
							
							
							
