<br />
浓烟呛人，烈焰炙烤，爆炸威胁。“那时候最大的问题不是怕，而是累！体力到了极限，手机械地转着方向盘似的阀门。”空气呼吸器太重，拿下了；戴手套打滑，脱了，手很快磨破了皮；渴、热，实在顶不住了，从水带接口处接点水洗洗脸、润润嗓子，再回来继续干！<br />
<br />
4个小时，8万多转！两个主要阀门终于被关闭。不久，前线指挥部调来专业供电车，现场多个阀门被关闭。<br />
<br />
不畏艰险、勇打阻击，主场作战的大连消防支队为后续增援决战决胜扑救赢得了宝贵时间！<br />
