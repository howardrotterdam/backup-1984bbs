<br />
7月16日晚。我国目前规模最大、水位最深的现代化深水油港――大连新港码头油库起火。储油区告急！开发区告急！大连告急！<br />
<br />
那一夜，辽宁公安消防官兵经历了血与火的考验，打赢了这场生死之战，刷新了多项历史纪录：<br />
<br />
15小时内控制并扑灭10万吨级油罐特大化工火灾，扑救时间最短；<br />
<br />
全省范围一次调集348台消防车、2300名消防警力，属辽宁首次；<br />
<br />
15小时扑救过程中无人员伤亡；<br />
<br />
首次超远距离供水装备实战应用；<br />
<br />
首次陆海空联合灭火作战。<br />
<br />
那一夜，八方支援、千里奔袭，整体协同作战，中国人创造了我国乃至世界火灾扑救史上的奇迹！<br />
<br />
7月16日18时12分：警报拉响<br />
<br />
果断处置、靠前指挥，是打赢生死之战的前提<br />
<br />
7月16日18时12分，火警传来――大连市大孤山新港码头保税区油库起火。<br />
<br />
18时19分，大孤山消防中队代理中队长刘磊率队赶到现场，陆续赶到的开发区7个中队官兵操起泡沫枪与流淌火展开“拉锯战”。但眼见火越打越大，火苗从粗大的输油管线撕裂开的大口子里蹿出，油助火势，一路狂奔，沿管线翻滚着向外扩散，泄漏的原油液在错综复杂的管线里汇合，一次又一次地形成新的着火点，再形成新的流淌火不断向外蔓延、扩散。“领导，压不住了！”一线指战员扯着嗓子向后方报告。<br />
<br />
虽然对化工火灾的严重性有思想准备，大连消防支队支队长丛树印完成火情侦查后，仍倒吸了一口凉气：“咱也是30多年的老消防了，从来没见过这么大的火！”<br />
<br />
直径80米、高20多米的103号油罐起火、爆炸、罐体坍塌；<br />
<br />
平均储油量达10万立方米的多个毗邻罐长时间被高温烘烤，罐与罐之间仅相距30―50米，有输油管道并联相通，随时可能发生连锁爆炸；<br />
<br />
多处输油管线连续发生爆炸，井盖、阀门等被抛向空中，现场浓烟滚滚，火光冲天；<br />
<br />
更可怕的是，距火场不足百米处有易燃易爆危险化学品仓库，一旦爆炸，后果不堪设想！<br />
<br />
一场恶战在所难免！大连市消防指挥中心第一时间启动重大灾害事故应急处置预案，迅速调集128台消防车、1000余名消防官兵赶赴现场。<br />
<br />
火情牵动中南海。胡锦涛总书记、温家宝总理作出重要指示。李克强、周永康等领导同志也作出指示。<br />
<br />
中共中央政治局委员、国务院副总理张德江代表党中央、国务院，连夜紧急赶赴事故现场，指挥灭火救援工作。<br />
<br />
国务委员、公安部部长孟建柱与前线指挥员多次通话提出具体要求，公安部立即启动应急机制。<br />
公安部副部长刘金国率领消防局局长陈伟明和消防专家连夜赶到火场最前沿，现场指挥一站就是7个小时。<br />
<br />
辽宁省委省政府、大连市委市政府领导迅速赶赴现场。省公安厅及消防总队领导一边赶路，一边迅速决定启动跨区域增援预案，三次调警，命令全省13个消防支队、14个企业消防队，以最好装备、最强人力、最快速度，向大连集结！<br />
<br />
在增援部门到来之前，必须阻止火势进一步蔓延！关阀断料是当务之急。“只有关掉阀门，才能切断输油管道的供油来源，也才能防止起火的原油回流油罐引起连环爆炸。”<br />
<br />
电动关阀只须几分钟，但现场配电室烧毁无电源可用。大连支队特勤二中队指导员桑武率攻坚小组冲入火场，在企业员工的协助下手动关阀。“企业的人说，手动十几二十分钟就能完事。真干起来，好家伙，1米多高的阀门螺丝，转80圈才前进一扣！”<br />
<br />
浓烟呛人，烈焰炙烤，爆炸威胁。“那时候最大的问题不是怕，而是累！体力到了极限，手机械地转着方向盘似的阀门。”空气呼吸器太重，拿下了；戴手套打滑，脱了，手很快磨破了皮；渴、热，实在顶不住了，从水带接口处接点水洗洗脸、润润嗓子，再回来继续干！<br />
<br />
4个小时，8万多转！两个主要阀门终于被关闭。不久，前线指挥部调来专业供电车，现场多个阀门被关闭。<br />
<br />
不畏艰险、勇打阻击，主场作战的大连消防支队为后续增援决战决胜扑救赢得了宝贵时间！<br />
<br />
7月17日零时23分：首支增援部队到达现场<br />
<br />
面对生死，敢于担当，是打赢生死之战的关键<br />
<br />
警灯闪烁、警笛长鸣，辽宁境内沈大、沈丹等高速公路上，一条条由消防车组成的“红龙”全速向大连方向挺进。大连金州公安分局调集15辆警车赶赴沈大高速三十里堡服务区，对所有外市来大连支援的消防车进行引导，同时派出交警沿途进行交通疏导，确保增援力量在最短时间内赶往现场。<br />
<br />
7月17日零时23分，邻近的营口消防支队增援力量首先到达现场。<br />
<br />
鞍山、盘锦、沈阳、丹东、本溪、辽阳、抚顺、葫芦岛……7月17日11时半，最远的朝阳消防支队增援力量从600公里外到达现场。“每辆消防车都拉着二三十吨的水或泡沫，高速路上大货车又多，我们的驾驶员还是跑到了120公里的时速，几乎达到速度极限！”朝阳消防支队副支队长王大伟说。<br />
<br />
各增援部队就位，前线指挥部迅速制定了“先控制、后消灭”的战术原则，划分战区，部署17辆消防车在油罐和毗邻的危险化学品库区之间打开一条防火通道；各扑火阵地利用水泥和沙土围堵外溢原油，以移动水炮和车载水炮对冷却受威胁罐体，采取泡沫喷射、沙土覆盖等方式压制和消灭火线、地面流淌火。<br />
<br />
营口消防支队进入被大火团团围住的大连港罐区，不远处就是危险化学品库区，中队长卢天昊、副中队长徐战龙率领两个攻坚组，采用水枪掩护、泡沫炮强行压制、泡沫枪覆盖推进的方法进入火场，立即消失在浓烟烈火之中，后面跟进的水炮车失去方向，驾驶员大喊：“中队长，车往哪开啊？”卢天昊吼道：“往火里开！”<br />
<br />
全省消防部队的拳头中队――沈阳启工中队被派往大连港罐区最危险的位置，任务是阻截烈火从阀组蔓延到油罐群，并贴近油罐阀组寻找漏点。指导员谢立峰带领8名参战官兵，冒着高温和爆炸的危险，以泡沫枪交替掩护，直攻漏点，用移动水炮成功将漏点的火势扑灭。战士张立新、郭人逢手持泡沫枪跪在阀组下战斗了9个多小时。<br />
<br />
盘锦市消防支队的主阵地是中国船舶输油码头，必须强攻冷却管道，防止爆炸。支队长鲁晓明立即安排灭火攻坚小组，使用泡沫炮压制火势。<br />
<br />
位于码头上面的103号储油罐爆炸后，猛烈燃烧的原油在排污口附近聚集成“火海”。码头下方海面上的船只已经起火燃烧，水面上原油也在猛烈燃烧。丹东消防支队支队长马伟明当即部署灭火阵地，官兵们在海上公安队的配合下，一举扑灭现场明火。<br />
<br />
鞍山消防支队参谋长杨宝良，全省资历最老、经验最丰富的战训业务骨干，身先士卒，冲在危险的最前沿……<br />
<br />
本溪消防支队作训参谋王刚，身患结肠癌，手术后正在化疗，主动请战……<br />
<br />
老兵在前，新兵在后。“攻坚小组有没有新兵？这节骨眼上，冲在前面的都是干部、技术骨干，最起码也是士官。”营口消防支队参谋长许永江快人快语。<br />
<br />
勇士无名，勇者无畏。在长达15小时的时间里，现场2000多名消防官兵用意志、用毅力坚守着各自的“阵地”：一人顶着七八层楼高的烈焰打水枪，后面一人朝他的身体喷水降温，两三分钟就得轮换一次；流淌火反反复复，指战员们抱着泡沫枪不停阻击，累得实在不行，就趴在地上打，渴了，喝水枪里的水，有的车加的是海水，喝到嘴里发涩，只能润润嘴唇；18岁的小战士负责供水，疲劳过度，头磕在消防车上晕了过去；冲锋在前的指导员不小心掉进满是油污的管线池，耳朵被烧坏；三名指战员负责运泡沫，一桶泡沫19―25公斤，一晚上一台车用将近4吨泡沫，三个人记不清搬了多少趟，实在搬不动了，就坐在地上用背顶着桶往前挪；一线官兵的战斗靴里都是滚烫的泡沫和原油，靴子和皮肤粘在一起脱不下来……<br />
<br />
在他们身后不远处，站着公安部刘金国副部长，站着现场总指挥、辽宁省公安厅长李文喜，大连市副市长、公安局长王立科……<br />
<br />
现场先后发生6次爆炸。每次爆炸过后，现场浓烟四起，将正在鏖战的战士们吞没，指挥员们的心就揪成一团。好在浓烟散去，只见战士们趴在满是油污的地上，安然无恙，一颗心才落了地。<br />
<br />
“怕不怕死？都是凡人，能不怕吗？没办法，谁让咱干消防员呢！职责所在。再说，领导跟我们在一块呢，还有党和国家多年的教育培养。”这是现场指战员们质朴的心声。<br />
<br />
“我们的战士太可爱了！十八九岁的娃娃，要是在家里，还在父母跟前撒娇呢！都说现在的兵不好带，关键时刻，他们经受住了血与火的考验，不愧为辽宁消防铁军的一员！”这是现场总指挥的由衷之语。<br />
公安部副部长刘金国率领消防局局长陈伟明和消防专家连夜赶到火场最前沿，现场指挥一站就是7个小时。<br />
<br />
辽宁省委省政府、大连市委市政府领导迅速赶赴现场。省公安厅及消防总队领导一边赶路，一边迅速决定启动跨区域增援预案，三次调警，命令全省13个消防支队、14个企业消防队，以最好装备、最强人力、最快速度，向大连集结！<br />
<br />
在增援部门到来之前，必须阻止火势进一步蔓延！关阀断料是当务之急。“只有关掉阀门，才能切断输油管道的供油来源，也才能防止起火的原油回流油罐引起连环爆炸。”<br />
<br />
