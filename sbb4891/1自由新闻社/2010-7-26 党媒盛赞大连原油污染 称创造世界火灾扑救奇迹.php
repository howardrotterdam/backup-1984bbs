<br />
面对生死，敢于担当，是打赢生死之战的关键<br />
<br />
警灯闪烁、警笛长鸣，辽宁境内沈大、沈丹等高速公路上，一条条由消防车组成的“红龙”全速向大连方向挺进。大连金州公安分局调集15辆警车赶赴沈大高速三十里堡服务区，对所有外市来大连支援的消防车进行引导，同时派出交警沿途进行交通疏导，确保增援力量在最短时间内赶往现场。<br />
