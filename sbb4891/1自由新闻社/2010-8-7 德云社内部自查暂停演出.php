													<h2>德云社内部自查暂停演出</h2>
						
						
																											<div id="postmessage_772956" class="t_msgfont"><a href="http://ent.sina.com.cn/s/m/2010-08-07/16243043528.shtml" target="_blank">http://ent.sina.com.cn/s/m/2010-08-07/16243043528.shtml</a><br />
<br />
新浪娱乐讯&nbsp;&nbsp;经历了一系列风波之后，8月7日凌晨，德云社发布通知称开始内部自查，暂停小剧场演出。而据相关部门介绍，此次德云社的自查并非主管部门要求，而是自发行为。新浪娱乐向负责人王海及相关人士求证具体情况，但均无回应。<br />
<br />
　　德云社的通知称：“为深入贯彻学习胡锦涛总书记在中共中央政治局第二十二次集体学习时的重要讲话精神及中共中央宣传部通知精神，北京德云社特决定自2010年8月9日(周一)起暂停所有小剧场(北京德云社剧场、德云社三里屯剧场、张一元天桥茶馆、广德楼剧场)的常规演出，按照讲话精神开展深入自查，进行内部整改。具体的演出恢复时间，将根据实际情况另行通知。”<br />
<br />
　　新浪娱乐了解到，德云社作为演出单位，由北京宣武区文委颁发演出团体和演出资质两证，不是曲艺家协会的会员单位，算“个体户”，也就没有上级领导。工商局和宣武区文委算是政府部门与之打交道较多的单位，但他们并未提出要求他们停业自查。德云社是看到文化部长蔡武8月6日接受媒体访问时对“反三俗”发表重要意见，而自行决定停业整顿的。<br />
<br />
　　对于自查的具体步骤和内容、目标等等，新浪娱乐试图向德云社负责人及相关人士求证，但至发稿时，均无回应。JZB/文<br />
