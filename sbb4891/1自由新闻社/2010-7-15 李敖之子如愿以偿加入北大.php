<br />
著名的台湾作家、时事批评家李敖的儿子李戡(kān)在今年的台湾地区大学入学考试学科能力测验(以下简称“学测”)中成绩优秀被台大录取，但是他放弃了台大申请北大，并于6月通过了北大的面试。昨天，北大招办确认，李戡已经被北大经济学院录取。<br />
<br />
　　根据政策，拥有台湾地区户籍，并且“学测”成绩达到顶标级(最优级)的高中毕业生就可以报考北大，北大会根据面试成绩和“学测”成绩择优录取。<br />
<br />
　　李敖的“国会研究室”秘书李小姐回复说，李敖本人确实听闻儿子被北大录取，如果榜单一旦发布，必然舍台大上北大，“这也源于儿子的心愿”。<br />
<br />
　　虽然只有17岁，但是李戡已经继承乃父之风，针砭时弊、文才出众。他的第一本书《李戡戡乱记》即将出版，内容就是比对各种教科书里扭曲的事实，抨击台湾教育的黑暗。<br />
<br />
　　据了解，北大今年接受了60份台湾应届高中生的入学申请，共有13名台湾学生被北大(含医学部)录取。<br />
<br />
　　&gt;&gt;李戡的北大申请书<br />
<br />
　　我想到祖国念最好的大学<br />
<br />
　　今年台湾的大学入学学科能力测验，总计考生140540人，我的成绩超越135864人。五月大学学测放榜，我已考取台湾大学。<br />
<br />
　　虽然考取了台湾最好的大学，虽然台湾是祖国的一部分，但我想到祖国念最好的大学，因为希望我是它的一部分。<br />
<br />
　　一如我的祖父、我的大姑、二姑、大姑父，都在北大毕业，我的父亲李敖以未念北大为憾，我愿我能超越这六十年的海峡。<br />
<br />
　　台湾是祖国的一部分，但它太狭小了，我写了一本书(今年八月出版)，一方面检讨台湾的狭小，一方面展示我辈的心愿。<br />
<br />
