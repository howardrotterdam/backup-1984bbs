																											<div id="postmessage_719526" class="t_msgfont">【世界日报u北京3日电】 2010.07.03 04:17 pm <br />
 <br />
&nbsp;&nbsp;<br />
 <br />
大批村民堵塞成绵高速公路。<br />
网路图片 <br />
四川大地震重灾区德阳市爆发大规模警民冲突，该市八角井镇5000村民因怀疑有官员侵吞当地的国企「东汽」赔偿村民重建款项，自6月27日起围堵成（都）绵（阳）高速公路与「东汽」抗议。当局派出1000多名防暴警戒备，期间与村民爆发多次严重冲突。有消息指事件造成最少300人伤，200人被捕。八角井镇镇政府则否认有官员贪污重建款项，灾区重建工作进展顺利。<br />
<br />
四川早前被揭有人用不合格的红砖兴建房屋。这次冲突事件主因是2008年「五一二」大地震发生后，设于绵竹的大型国企东方汽轮机有限公司厂房严重破坏，当局投资50亿元在该镇征地重建厂房。按村民计算，他们每户最少应得26万元补偿，但每户村民最终却只拿到3万元，怀疑有官员从中贪污。 <br />
<br />
老人受伤 不治身亡 <br />
<br />
为争取合理赔偿，5000名村民6月27日起围堵成绵高速公路和「东汽」抗议，促当局交代补偿款项事宜。村民的堵路抗议行动造成绵高速公路的交通瘫痪，为免抗议造成更大的负面影响，当局连日派出逾千名防暴警察和四辆装甲车到场戒备。<br />
<br />
中国人权民运信息中心指出，截至昨日，警方与村民最少发生三次大规模冲突，造成300人受伤，200人被捕。八角井镇医院也称，目前大约有100人在医院留医，当中许多人骨折。还有村民说，有一名老人在冲突受伤，最终不治身亡。 <br />
<br />
封锁消息 警车被砸 <br />
<br />
有八角井镇村民在网上发帖，当局为封锁消息，特别派警员在夜间采取行动驱赶村民，让村民无法拍照。有多名村民在日间用手机偷偷拍摄，结果也被警方发现，当场将其手机砸烂。而在冲突过程中，警方曾发射催泪弹，村民则用石头、砖头还击。其中在6月27日晚的冲突尤为激烈，有大量村民流血受伤，还有两辆警车被砸。翌日早上，当局要出动消防车清洗现场的血渍。 <br />
<br />
地方政府 否认冲突 <br />
<br />
据了解，由于当局一直没有出面交代补偿款项，至今仍有村民在「东汽」门前搭起帐篷抗议。 「东汽」有关人员昨在接受查询时也承认被村民围堵，但八角井镇政府工作人员却否认发生冲突事件，指从没接到村民反映有官员贪污，又强调重建工作在正常进行。</div>

							
							
							
							
