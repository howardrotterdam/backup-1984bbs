																											<div id="postmessage_817841" class="t_msgfont">江苏版语文教材删除《阿Q正传》等文<br />
2010-09-07 14:36:48　来源: 中国新闻网(北京)　 <br />
核心提示：新学期江苏版中学语文课本删除了《雷雨》、《孔雀东南飞》、《阿Q正传》等众多名篇。负责人表示，“所选篇目不再以意识形态划分，而是让语文教材回归到语文、回归到人文。”<br />
<br />
中国新闻网9月7日报道 新学期伊始，语文教材收录了哪些文章再次成为人们关注的焦点。<br />
<br />
昨天，一则《各地中学教材大换血，看看被踢出教材的课文》的帖子在网上广为流传，《孔雀东南飞》《阿Q正传》《雷雨》等20多篇承载几代人记忆的课文从教材中消失，引起了不小的争议。<br />
<br />
正方：教材更新很正常<br />
<br />
“新的时代需要新的经典，这是一个不断更新的过程。”支持语文教材“大换血”的网友表示，教材的更新很正常。“我们过去的教材编写不尊重学生的口味需求，不尊重教学需要，导致审美功能与人文精神缺失。而现在正在进行还原文学审美本质功能的一次尝试，更注重时代元素，更关注人文精神，更强调教材编选内容的丰富性，这实际是对现行语文教材改革的一次有益探索。”一位网友说。<br />
<br />
反方：教材求新是剑走偏锋<br />
<br />
不少网友强烈反对语文教材有如此大的改动。“编写教材，要用一种长远和宏观的目光来选择教材内容，既要能够传承文明，又能够面向未来。语文教材大换血可以，但不应把原先拥有顽强生命力的传统篇目撤换殆尽，这对语文教学的改革不啻为一种灾难。”<br />
<br />
中立：新增的篇目也挺好<br />
<br />
有网友认为应该客观地看待教材“换血”一事，他认为语文教材新增的篇目也有辛弃疾的《水龙吟》、戴望舒的《雨巷》等经典作品。编剧柏邦妮在博客上撰文说：“ 大家纷纷指责这么好的东西不应该全部抛弃。可是我想到了一个问题，于是问：‘拿出去什么并不重要，重要的取而代之的是什么？’”看到事物的两个面，就会发现，其实，并没有那么糟糕。”<br />
<br />
苏教版中学语文教材组负责人详解编撰过程――<br />
<br />
【链接】<br />
<br />
“让语文教材回归到人文”<br />
<br />
各地语文教材变化<br />
<br />
南京的语文教材用的是江苏教育出版社的新版。从2000年开始，江苏教育出版社的“新课改”教材进入实验区，经过不断修订，近两年已经基本稳定，目前已在江苏、浙江、山东、山西等地广泛使用。昨天，苏教社中学语文教材组相关负责人余立新接受记者采访时表示：“今年苏教版的高中语文教材和去年基本没有变化。” 帖子中提到的一些“被撤课文”，苏教版也未收录。“语文教材有变化是正常的，不必太过敏感。”<br />
<br />
从去年开始，苏教版语文教材也增加了许多新篇目。“必修教材中有50%的篇目、作家都是过去语文课本中没有的。”余立新告诉记者，古代的经典太多了，无论选什么篇目都很正常，而近现代的作品，在学术上还存在着“经典重估”的问题，所以教材的编撰者选择哪些篇目都可能引起争议。苏教版语文教材去年就增加了海子、韩少功、曹文轩、周国平等诗人、作家的作品，学生们的反响都还不错。他认为，“所选篇目不再以意识形态划分，而是让语文教材回归到语文、回归到人文。”<br />
<br />
余立新告诉记者，新课改实行后，学生高二上半学期期中考试之后，就开始上选修课了，选修教材及配套的读本中有许多未被教材收录的课文。“读本收录的作品自由度比较大，各种声音、各种风格的文章都有。”他建议，学生应该进行更多教材之外的阅读，“阅读是提升语文水平、人文素养最好的途径，现在的阅读应试性太强，学生的自主阅读才是真正的阅读。”<br />
<br />
<br />
