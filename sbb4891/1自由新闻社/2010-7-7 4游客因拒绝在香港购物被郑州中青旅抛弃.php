													<h2>4游客因拒绝在香港购物被郑州中青旅抛弃</h2>
						
						
																											<div id="postmessage_727131" class="t_msgfont">4名游客因拒绝在香港购物被旅行社抛弃<br />
<br />
2010年07月07日10:45&nbsp;&nbsp;大河网-大河报<br />
<br />
<img src="http://i3.sinaimg.cn/dy/s/p/2010-07-07/U4126P1T1D20627840F21DT20100707104532.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
旅行社给游客发送的短信。<br />
<br />
　　本报讯 “我们洛阳四个游客现被困在白云机场。郑州中青旅组团到香港旅游，因我们不愿购物，非让每人补交2000元。我们没交，旅行社就把我们从旅游团里甩了，原定要到澳门旅游的，现在我们被困在广州机场。”昨天上午10时，洛阳市张女士等人向本报投诉。<br />
<br />
　　游客 不想购物被旅行社甩了<br />
<br />
　　据张女士介绍，她们四人6月30日在郑州中青旅报的港澳五日游，这个团共20人。事先她们与洛阳风采旅行社签了合同，到郑州后每人给中青旅交导游小费420元。她们和导游说好不能强迫购物，但到港后导游却说：“你们想想，报1000多元的团连机票都不够，更不用说到香港吃啊住啊。”她们不购物，导游当场让她们交钱，并说，不想买东西可以，交2000元，就可自由活动。她们没交钱。7月2日早上，导游要收她们通行证时，她们四人被迫离团跑了。当天下午到深圳，住三天。旅游团准备6日下午5点到白云机场集合，然后到澳门。昨天下午5点她们到机场等候，当她们去办登机牌时，才知她们的机票被取消了。<br />
<br />
　　与张同行的邓女士说：“我们在机场遇到同团的游客，他们说，‘你们走后，可把我们坑死了，每人强迫消费1万多元。’我们到机场发现机票被取消后报了警，警方与旅行社张经理联系，他说不管。广州公安部门让导游王芬芬留下，与我们协商如何处理，想不到的是张经理连他的员工也不管了。经理说导游也不用回了，让导游跟着我们四个吃喝住。昨晚9点导游也跑了。我们感到很无奈，我们都是第一次出门，对广州情况一点都不熟悉，现在被困在机场。”<br />
<br />
　　随后，张女士把7月2日旅行社张经理发给她的短信转发给记者(如图)，内容为：“您好，现因您的擅自离团，给我方造成诸多不便及经济损失，需补收损失及违约金2000元/人；请与本日12点前联系报名旅行社，逾期我方将取消您的回程机票。此外，我方将向香港入境处提交拒绝您再次访港的申请。以上后果均由您承担，请三思！”<br />
<br />
　　张女士表示，旅行社的行为已违反了合同，合同上没有必须购物之说。假如她们在国外，旅行社难道把她们甩在国外不管吗？她们在深圳住3天钱都花完了，现在连回家车票都没钱买。她们要求中青旅补偿她们的各项损失，包括在深圳、广州的住宿费、餐费和乘车费等。<br />
<br />
　　旅行社 是客人单独解除了合同<br />
<br />
　　郑州中青旅张经理承认给她们发了上述短信，张经理表示，客人没按行程的话，应补交旅团费用2000元。他们是与洛阳风采旅行社合作的，这4人是洛阳送来的。她们第一天跟旅游团游览了景区，按正常安排次日上午在港购物，早上7点40分她们就走了。客人单独解除了合同，旅行社把回程机票取消了。张经理说：“在香港让交2000元的事我知道，是她们自己先走的。”<br />
<br />
　　省旅游局 旅行社不能强迫游客购物<br />
<br />
　　昨天记者就此采访了省旅游局行管处王处长。王处长表示，这主要看合同是怎么约定的，合同约定有购物，游客得到购物点去，想买东西自己买，不想买也可以不买，但旅行社不能强迫游客购物。按《旅行社条例及实施细则》规定，游客不购物，旅行社不能要求游客每人必须交2000元。如合同上没有购物，硬强迫游客购物，游客回来后可向旅游局质监所投诉旅行社。<br />
<br />
　　截至记者发稿时，张女士来电说她们没办法，让一女同事的老公给打来款，准备买火车票返回河南。<br />
