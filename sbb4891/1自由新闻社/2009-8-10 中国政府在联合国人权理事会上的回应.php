													<h2>中国政府在联合国人权理事会上的回应</h2>
						
						
																											<div id="postmessage_198334" class="t_msgfont">提要：中国代表团在联合国人权理事会针对中国的人权状况进行普遍定期审查的会议上所作回应<br />
<br />
在联合国人权理事会2009年2月9日对中国的人权状况进行“普遍定期审查”的会议上，对于其他国家对于中国的“言论自由”、“对待人权倡议者的态度”、“集会自由”、“宗教自由”、“劳改制度”、“法外拘留设施或'黑监狱'”、“中国律师以及拘留者的权利”、“少数民族（藏族，维吾尔族）”、“死刑统计数字”、“工人权利”这些议题所提出的质疑以及建议，中国代表团所给出的回复如下：<br />
<br />
<br />
言论自由<br />
中国代表在回复其他国家对于中国公民的“言论自由”状况的质疑时，声称在中国“没有审查制度、没有任何个人或媒体因为表达自己的意见或看法受到处罚、记者工作中所面临的障碍不是来自政府、法律禁止在互联网上制造谣言或颠覆行为都符合《公民权利和政治权利国际公约》的规定。”<br />
<br />
在回应其他国家所提出的针对中国“言论自由”状况所提出的建议时，中国不支持：“扩展对外国记者放宽限制的做法让中国记者也能这样。修改违反言论自由的法律和惯例以及释放因为这类罪行而被关押的囚犯（包括那些与08仙章有关联的人）。接受人权捍卫者通过和平示威所表达的不同意见”<br />
<br />
<br />
对待人权倡议者的态度<br />
在回复“对待人权倡议者”这方面质疑时，中国代表团的回应是“国家保密法没有被滥用去惩罚人权捍卫者，做用于保护国家机密的法律明确规定了“国家机密”的范围（暗示不会对“国家机密”进行扩大化的解释）”<br />
<br />
在回应其他国家所提出的针对中国“对待人权倡议者”这个议题所提出的建议时，中国不支持：“改革保守国家秘密法以及诸如煽动颠覆这样的犯罪定义，以防止滥用这些法律去惩罚人权捍卫者。人权捍卫者遭骚扰和拘留的调查报告。”中国支持：警惕 “认为其本身是合格的人权捍卫者而其目的却是攻击国家利益和中国人民的那类人士。”<br />
<br />
<br />
集会自由<br />
在回复其他国家针对中国的“集会自由”状况所提出的质疑时，中国代表团的回应是：集会的自由，结社的自由，游行的自由，示威的自由这些权利都是受保护的并且公民可以在法律规定底下去申请参加这类活动。<br />
<br />
在回应其他国家所提出的针对中国“集会自由”状况所提出的建议时，中国不支持：确保去保护和平集会的权利并且释放所有与此类活动有关的被捕人士<br />
<br />
<br />
宗教自由<br />
在回复其他国家针对中国的“宗教自由”状况所提出的质疑时，中国代表团的回应是：对于宗教组织的登记要求是“最低化的”并且基督徒的“家庭聚会”是不需要登记的<br />
<br />
在回应其他国家所提出的针对中国“集会自由”状况所提出的建议时，中国不支持：审查范围涉及（具体的）宗教团体，包括非法的宗教团体；保障宗教自由，信仰自由，以及私下做礼拜的自由；简化官方批准宗教实践的要求。<br />
<br />
<br />
劳动改造制度（TTL，这种体制在司法体制以及刑事诉讼法以外进行，最高允许有4年的拘留）<br />
在回复其他国家针对中国的“劳改制度”状况所提出的质疑时，中国代表团的回应是：中国的劳改体制与其他国家的惩教服务体制是类似的，适用于那些犯了罪，但不值得进行刑事判决的对象。中国有320个劳改中心以及190000名劳改犯人。<br />
<br />
在回应其他国家所提出的针对中国“劳改制度”所提出的建议时，中国不支持：废除劳改体制。中国支持：根据自己的国情改善劳改体制。<br />
<br />
<br />
法外拘留设施或“黑监狱”<br />
中国代表在回复其他国家对于法外拘留设施或“黑监狱” 状况的质疑时采取的说法是： “我们国家没有黑监狱。” <br />
<br />
在回应其他国家针对中国法外拘留设施或“黑监狱”所提出的建议时，中国的表态是不支持：废除黑监狱（官方的逻辑是既然是不存在的东西怎么需要去废除呢？）。<br />
<br />
<br />
中国律师以及拘留者的权利<br />
在回复其他国家对于“中国律师以及拘留者的权利” 这方面质疑时，中国代表团的说法是：最近修改的律师法清楚的保护着律师的权利，他们的人身自由以及免受在刑事诉讼中为他们客户进行辩护而遭受到的制裁。在国家机密的案例中，对律师与其代理人进行会面时设限制是很正常的事情。<br />
<br />
在回应其他国家针对“中国律师以及拘留者的权利”所提出的的建议时，中国代表团的表态是不支持：确保律师“可以替代理人辩护而不必担心受到骚扰以及律师能够参与管理他们自己的专业组织”。确保司法以及律师的独立性。确保拘留者会客的权利以及拥有“能永久获得法律咨询和有效投诉机制”的权利。<br />
<br />
<br />
少数民族（藏族，维吾尔族）<br />
在回复其他国家对于“少数民族（藏族，维吾尔族）”政策所提出的质疑时，中国代表团的说法是：<br />
少数民族的宗教信仰和文化是受到尊重和保护的。少数民族可以充分行使他们的人权。“极少数人在外国势力的支持下试图把西藏和新疆从中国版图中分裂出去，他们绝不代表大多数西藏人和维吾尔人因为西藏和新疆是中国领土不可分割的一部分，政府不会允许任何试图分裂中国的图谋得逞。<br />
<br />
在回应其他国家针对“少数民族（藏族，维吾尔族）”所提出的的建议时，中国代表团的表态是不支持：审视法律和惯例以确保对于少数民族宗教信仰自由（1），（2），迁徙、文化和语言的保护。结束与众多人权侵犯行为相关联的在西藏进行的“严打”运动。为人权事务高级专员（OHCHR）的办公室、联合国其他机构，外交人员，和国际媒体增加进入藏族地区的机会。中国支持：继续“努力以进一步确保少数民族包括文化权利在内的各种人权。”<br />
<br />
<br />
死刑统计数字<br />
在回应其他国家针对“中国死刑统计数字”所提出的的建议时，中国代表团的表态是不支持：让死刑统计数字公开的做法。<br />
<br />
<br />
工人权利<br />
在回复其他国家对于“中国工人的权利”这方面质疑时，中国代表团的说法是：宪法和工会法的规定是，工人可以“完全自由的组织和参加工会并开展活动。”<br />
<br />
在回应其他国家针对“中国工人的权利”所提出的的建议时，中国代表团的表态是不支持： <br />
取消对《经济，社会及文化权利国际公约》（ICESCR）第8.1条（a）规定有所保留的做法，“那条规定确保人人有权组织工会和参加他（或她）所选择的工会。”<br />
