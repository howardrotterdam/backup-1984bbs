													<h2>联合国人权捍卫者函递给中国政府的指控信</h2>
						
						
																											<div id="postmessage_205993" class="t_msgfont">联合国“[关注]人权捍卫者处境特别报告员” Margaret Sekaggya <br />
<br />
向人权理事会 提交的报告（2007-2008） <br />
<br />
<br />
附录&nbsp;&nbsp;[节选]：<br />
<br />
函递给中国政府的指控信和紧急呼吁以及政府的回复（概述） <br />
 <br />
<br />
<br />
【编译者注： <br />
<br />
<br />
“[关注]人权捍卫者处境特别报告员”是联合国人权理事会任命的独立专家。现任Margaret Sekaggya 女士是一位来自乌干达的法官及国际人权法专家 （详细介绍请见<a href="http://www2.ohchr.org/english/issues/defenders/index.htm" target="_blank">http://www2.ohchr.org/english/issues/defenders/index.htm</a>）。该报告员每年向人权理事会提交一份年度报告，并附上报告员年内给世界各国政府提出的要求调查迫害骚扰人权捍卫者的具体个案的信件和呼吁书（以及收到的回复）。2007-08年度报告全文（英文）：<a href="http://www2.ohchr.org/english/issues/defenders/docs/A.HRC.10.12.pdf" target="_blank">http://www2.ohchr.org/english/is ... ocs/A.HRC.10.12.pdf</a> <br />
<br />
<br />
这里翻译发布的是这个附件的中国部分（附件全文：<a href="http://www2.ohchr.org/english/issues/defenders/docs/A.HRC.10.12.Add.1.pdf" target="_blank">http://www2.ohchr.org/english/is ... HRC.10.12.Add.1.pdf</a>）。 <br />
<br />
<br />
在2007-2008年间，许多民间人权组织（包括维权网）向特别报告员提出许多诉求和举报，要求对有关侵权个案进行调查或提出抗议。特别报告员根据信息来源的可靠性、完整性和准确性，决定是否采取后续行动。 2007-08年间， 特别报告员一共向中国政府呈交了42份信函，要求政府对被指控的侵权个案进行调查、作出说明，或者向政府提出“紧急呼吁”、要求立即采取行动停止侵权或追究责任。有些“紧急呼吁”是特别报告员给其他人权特别程序联合发出的，如“任意羁押工作组”、“酷刑特别报告员”、“言论表达自由特别报告员”、“宗教自由特别报告员”等等。 <br />
<br />
<br />
需要注意的是，中国政府对半数以上的信函做了回复。 这些回复对特别报告员的指控和紧急呼吁进行了全面质疑、否认、或辩解。民间人权团体应该针对这些否认和辩解之词进行回应，进一步引起公众舆论关注。这一博弈过程是国际人权机制通过公众舆论施压的一个途径。对于那些公开自诩“尊重人权”的政府，民间应该充分有效地使用这一工具去推动政府切实保护人权。此外，MargaretSekaggya 女士于2008年11月向中国政府提出希望访华的要求，至今没有答复。 <br />
<br />
<br />
---- 维权网 】 <br />
 <br />
<br />
<br />
一、以下是中国政府对联合国2007年12月11日之前呈递的“指控信”和“紧急呼吁” 的回复： <br />
<br />
<br />
445. 中国政府在2008年1月15就2007年11月5日关于毛恒凤的一个紧急呼吁的答复中，政府表示毛自从被羁押后，是与另外两名罪犯一起被羁押于一个房间，而不是单独关押。毛恒凤从未被其他女囚殴打，，也没有遭受任何“擦伤和割伤。毛在进入拘留所时候的体检表明她患有高血压，但是其他指标正常。经过在医院的治疗，她的血压已现在已经恢复正常。她最近一次全面的体检是在12月初，，虽然接受治疗，但是毛恒凤从来没有受到任何强迫药物治疗。我们的通讯上指责的毛恒凤咋监狱里受到虐待没有任何事实的支持。监狱管理人员根据法律规定对待囚犯，毛恒凤也和其他犯人一样享有同样的权利，她的健康权，她提出上诉的权利和所有其他未被剥夺的合法权利都受到法律保护。自从她被带到拘留所后，她每月接受家庭成员的访问，其中包括她的丈夫和女儿。迄今为止，毛恒凤本人没有提出书面申诉，也没有要求会见律师。<br />
<br />
446. 在2008年1月15日的回信中，中国政府回复了2007年10月9日关于郑大靖的紧急呼吁。政府答复说，在2000年12月20日，中国银行“云溪”分理处和郑大靖的就业合约终止，并一次性给予郑补偿金59,133.33元。合同终止后，郑最初继续占据单位分给他居住的单层房子，并随后转租给另一租客。 2004年，郑要求允许参与住房制度改革，并购买其居住的的单层房屋。但是因为他已经终止与工作单位的雇佣合同，因此他的问题不属于住房制度改革范围，工作单位拒绝了他的要求。在2004年6月，中国银行十堰市分行把云锡本地分行和管理下的资产，按照法律规定通过拍卖的方式进行出售，其中包括郑居住的这个房子和土地。依照法律规定，银行着手进行转让所有权手续。在2005年5月21日，购买方张贴通知要求郑腾空其居住的房屋，重建工作将开始。<br />
<br />
郑大靖认为，他有一个和中国银行十堰分行的租赁合约，他有优先购买的权利。然后，他向法院提起诉讼，要求法院命令中国银行十堰分行取消销售，取消所有权的转让，确认他对财产的优先拒绝权。<br />
<br />
在案件一审中，十堰市地方法院认为他原来的工作单位已经与郑终止了就业的关系，他的地位已经成为了一个非雇员，他不再有对该财产拥有任何权利，也没有任何证据表明，他有一个与工作单位的租赁协议。法院驳回了他诉讼请求。郑拒绝接受法院的裁定，并且提出上诉。2005年12月，十堰市中级人民法院作出判决，驳回了上诉，维持原判。<br />
<br />
为了解决郑的住房问题， 2007年6月27日，地方当局拨款购买了住房并免费提供给郑居住，并预留足够的住房和一次总付的搬迁费用。郑不接受这一安排。鉴于郑生活困难的情况下，在2002年第二季度开始，地方当局给他发出最低社会福利资助。<br />
<br />
郑拒绝接受法院的裁决，多次前往北京提出上诉。在2007年6月14日，郑和其他上访人聚集在最高法院提出申诉，并与保安人员发生冲突。公安机关以郑的行为涉嫌扰乱社会秩序调查了郑的行为，但他既没有被刑事拘留，也没有被逮捕，也没有受到殴打或虐待。自2005年以来，郑大靖的妻子曹祥珍陪同她的丈夫去北京上访，并带上了她的女儿。 当局和曹祥珍接触， 指出，她的女儿应该暂时委托家人或朋友照顾，但曹认为，她的女儿郑临沂仍然非常年轻，无法供养自己，因此曹必须把孩子带在身边。毫无疑问，指控曹和她女儿一起被拘留的说法没有任何可能。<br />
<br />
考虑到郑林欣伴随她父母长期在外，郑临沂错过了很多学校课程，在地方当局的安排下，专门指定了两名学校教师给她在暑假期间提供补习，费用是一个特别优惠的费用。<br />
<br />
