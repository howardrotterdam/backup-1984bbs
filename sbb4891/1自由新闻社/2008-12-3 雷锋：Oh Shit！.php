													<h2>雷锋：Oh Shit！</h2>
						
						
																											<div id="postmessage_7030" class="t_msgfont">穆正新：看雷锋积肥假案怎样戏弄公众智力<br />
<br />
<br />
雷锋弄虚作假的种种事迹，常在最浅显的水平上侮辱大众的智力。<br />
我们知道，在一般的造假案中，作案人不敢在浅显的水平造假。太浅显的造假一下子就会被人看穿。因此他们总会在比较重要的情节上下些功夫，让它们看上去符合常识，合理可行。下这些功夫多少反映出作案者对大众智力的敬畏。他们是在承认“世人并不好糊弄”的前提下作案的。<br />
<br />
&nbsp;&nbsp;但雷锋案、黄继光案以及大量其他共产党造假案有一个重要的不同点，那就是作案人公然藐视世人的智力。他们不考虑或不在乎上述前提。因为他们知道共产党打残了大众的智力，使人民大众无力识别或者不敢挑剔他们的骗局。只要党给他们撑腰，事情再假也会永远安全。因此他们作假的前提仅仅是：有党撑腰。这就够了。<br />
<br />
&nbsp;&nbsp;对于一般造假案，因为案中的假情节大体符合人类常识，公众不容易直接看出虚假。因此破解这种假案比较依赖于内幕知情者的曝光。例如美军“英雄女兵林奇”案中，如果所有当事人都坚持说林奇有英勇反击行为的话，美国大众很难认定林奇事迹有假。直到她本人说她当时其实没有还击时，公众才知道真相。<br />
<br />
&nbsp; &nbsp;而对于雷锋、黄继光式假案，外界通常不需要当事人揭秘，只凭着普通人的常识即可破案。因为这些假案的情节过于离奇，超出了合理可行的范围。以“林奇英雄案”为例，假如该案一开始的报道冒出“危急时刻林奇连开数枪打死五、六十个伊拉克匪兵”这种牛B描述的话，公众当即就能看出虚假，不必再等当事人吐露真相。雷锋积肥假案，就具备这类超出常理的荒诞情节。<br />
<br />
<br />
&nbsp;&nbsp;一、两次“春节捡粪”。<br />
<br />
&nbsp;&nbsp;雷锋多次声称他捡粪支援人民公社。光是入伍之后就有两次“春节捡粪”。<br />
<br />
&nbsp;&nbsp;在1960年的一次录音报告中，雷锋说：“比如，我在过去的一年当中，我想到，一定要在新的一年当中，多做更好更多的成绩。因此，我连过年所放的假都没有休息，我去捡大粪，初一初二那两天我一共捡大粪600来斤。我想到这也是响应党的号召，大积肥，也搞了卫生运动，也能够促进农业生产。”(这次录音报告以“ 一辈子学习毛主席著作”为题收入《雷锋全集》)<br />
<br />
第二年，1961年2月15日，雷锋记述道：“今天是古历大年初一，全连的同志都高高兴兴地到和平俱乐部看剧去了，我呢？为了在春节期间给人民做一件好事，吃过早饭后，我背着粪筐，拿着铁锹到外地拣粪，大约拣了300来斤粪，我送给了抚顺望花区工农人民公社，……” <br />
&nbsp;&nbsp;两天后的2月17日 ，雷锋在日记中又写道：“今天是春节假期的第四天，吃早饭的时候，……我一边吃饭，一边想：春节五天假期过完了，19号就要开始冬训。为了响应党的号召，支援农业第一线，争取今年农业大丰收，我还是去多积点肥，支援人民公社，这样做有两个好处。第一，以实际行动支援农业，对社员们是一个鼓舞，同时也更密切了军民关系。第二，替居民搞了卫生。因小孩在屋前屋后拉了很多大粪，看起来脏得很，我去把大粪捡起来，给居民把地扫干净，这真是一件一举两得的好事，既搞了卫生又积了肥。说干就干，我推着手推车，拿着铁锹和粪筐，走到了望花区北后屯，看见了工人住宅的屋前屋后有很多一小堆一小堆的粪便，我便立刻捡了起来。……到了下午2点钟，我捡了满满一车粪，送给了望花区工农人民公社。人民公社的负责同志们都很受感动。……”<br />
<br />
&nbsp; &nbsp;雷锋自述的这两次“春节捡粪”共计四天。即每个春节出动两天，其中三天日均产量300斤。最后一天产量是“满满一车”，估计也有300来斤。第一次“春节捡粪”应该发生在辽宁营口市。因为那时雷锋刚入伍才二十天，还在营口市的新兵团受训。第二次发生在抚顺市望花区。如果这两次“春节捡粪”事迹属实，则在两年的春节期间，辽宁省营口和抚顺两市雷锋驻地附近地区要分别提供600斤左右散落于房前屋后的人粪便。这样的任务毫无疑问会使两地居民度过历史上最肮脏的春节。<br />
<br />
<br />
&nbsp;&nbsp;二、成全雷锋一个人，搞臭望花半座城<br />
<br />
&nbsp;&nbsp;为了编造“助人为乐”事迹，雷锋要在城市里捡粪。但城市不是捡粪的适当场所。捡粪通常是中国农村地区的劳动。农村地区有许多牲畜在野外放养或者役使，从而提供了一定量散落的粪肥。城市里并不具备这种条件。那个时代里，一般城市还允许骡马大车进入市区。但牲口所散落粪便通常由赶车者搜集，或由城市环卫工人扫除。不可能保留在街道上供雷锋去捡拾。雷锋们看来也不敢指望骡马牛羊猪们在春节期间开进城来帮助雷锋做好事，只好去打“大粪”即人粪便的主意。但是人类恰恰是最不愿意随地排放粪便的动物。即便在农村地区也是这样。中国农民们视人粪便为重要农家肥，非常注意搜集。他们平常总是尽量使用自家茅房，除此之外还四处搭建简易茅厕以搜集他人粪便。捡粪的人在农村地区可能捡到不少牲畜粪便，但很难捡到人粪便。而要在城市里捡“大粪”就更困难了。在城市里，你出钱都不一定能找到人愿意当街脱裤子解大便。雷锋们欲在市区里捡出几百斤大粪，难度实在太高。<br />
<br />
&nbsp;&nbsp;但那是个“人有多大胆，地有多大产”的时代。雷锋们发扬着时代精神，决意造假。有大粪要捡，没有大粪创造大粪也要捡。从雷锋日记所写内容看，他们知道打成年人的主意并不现实。但这不妨碍他们发挥想象力去开创大粪来源。他们把希望寄托给祖国的下一代：“小孩在屋前屋后拉了很多大粪，看起来脏得很”。让小孩去干脏活，创造条件让雷锋做“好事”。这就是他们的主意。他们大概以为这主意比较稳妥。但事实证明这仍然是个很馊的主意。因为它经不起起码常识的检验。<br />
<br />
&nbsp;&nbsp;正常情况下，成年人一天排放的粪便量为一百至三百克，小孩的最多百来克。风干后的小孩粪便也就二、三十克。如果要凑足雷锋所需的粪便量，即每个春节300公斤，则营口和抚顺两市要在各春节里分别备好约三千份新鲜儿童粪便或者一万多份风干的儿童粪便。<br />
<br />
&nbsp;&nbsp;如果以风干儿童粪便满足雷锋“春节捡粪”做“好事”需要的话，则需要500名左右的儿童从腊月初八开始天天到室外排便，直到正月初一。500家的父母如此狠心缺德，自己用着暖暖和和的室内的厕所，却在滴水成冰的季节里天天把孩子逼到室外去排便。不知道这叫什么世道？在此期间，有关部门还需要对这些“雷锋预定捡粪场所”进行一定的管理。既要防止儿童不小心踩坏了“产成品”，更要防止爱干净的人一不小心把保存多日的粪便当作垃圾给打扫清除掉了。春节前大扫除本来是中国人承传了千百年的老传统。现在为了雷锋也必须暂时废止。不但人要配合，老天也要配合:老天不能在这期间里下大雪，否则冰雪会掩盖住粪便。如果解放军装备部不能紧急研制出性能优越的“探粪器”的话，雷锋就无法完成任务。<br />
<br />
&nbsp;&nbsp;你看，为了让雷锋同志顺利地做成这件“好事”，需要方方面面多少人们加上神们辛辛苦苦默默无闻地工作呀。<br />
<br />
&nbsp;&nbsp;那么，用新鲜儿童粪便可以省事一些吗？好像更糟糕。设3到9岁儿童为能够在室外顺利排便的“适龄儿童”。他们约占总人口的5%。这就是说，如果要以新鲜儿童粪便满足雷锋“春节捡粪”做“好事”需要的话，则必须在初一和初二（或初三）两天的上午把一个总人口约三万的小区里的全部约1500名“适龄儿童”都动员起来冒着严寒去室外排便。请问这将是怎样一幅臭气弥漫的春节景象？这样大规模的动员没有行政机构出面是做不到的。谁敢相信，当年营口市和抚顺市某区政府组织过这样规模的“为雷锋叔叔拉泡屎”的春节爱心奉献活动？天下任何一个政府的领导人，需要吃下什么药才能做出这种决策？<br />
<br />
&nbsp;&nbsp;考虑到多数父母心疼不忍以及孩子怕冷等等因素的影响，被动员区域内的小朋友也不可能都出动参加“为雷锋叔叔拉泡屎”的活动。如果真搞这样的活动的话，出勤率必然是很低的。这一来，为了保证雷锋完成任务，就只能扩大动员区域。为此将拥有十万人口以上的居民区列入动员是完全可能的。这已经相当于当年抚顺市望花区总人口的一半。<br />
<br />
&nbsp;&nbsp;成全一个人，搞臭半座城。这能算“助人为乐”吗？小朋友们可能要问：雷锋叔叔，您这“好事”怎么做得这么脏啊？如果雷锋没有因公殉职，而是接着往下做这种“好事”的话，岂不是要造成“雷锋春节到哪里，哪里粪便遗满地”的脏乱差景象吗？<br />
<br />
&nbsp; &nbsp;你会说，本文所设想的情节很荒诞。你说得对。但雷锋白纸黑字写下的这种“好事”，不借助荒诞手段能完成吗？谁能拿出合理可行的办法？<br />
<br />
<br />
&nbsp;&nbsp;三、图片为证<br />
<br />
&nbsp;&nbsp;雷锋“春节捡粪支援人民公社”事迹，毫无疑系编造而成。而且他们编造的东西太离谱，根本无法实现。但雷锋所在部队为了迷惑大众，还专门拍摄了相关题材的照片。照片作者季增更在接受《解放军报》采访时欺骗公众，把虚构摆拍的剧照说成是真实情况记录。<br />
<br />
<img src="http://docs.google.com/File?id=dhdvgbjk_470fv5z9crx" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;《解放军报》2001年3月5日 第7版刊登“我给雷锋拍照片的故事”一文。季增在文中谈到以下这张照片摄制过程时说：“一天早操前，我见雷锋挑着粪筐出去捡粪，便赶紧跑回办公室拿相机。等我追到郊外找到雷锋时，雷锋已捡了满满的一筐粪往回返，当雷锋又把粪倒往营房旁的粪堆上时，我拍下这一镜头。”<br />
<br />
&nbsp;&nbsp;但是这张照片恰恰是雷锋们弄虚作假的证据。在照片里，我们看到雷锋所“捡”的粪明显不一致。除了顶上正从粪筐里倾倒出来的几块“招牌”粪便具有人粪形状外，下面的整整一堆都是骡马粪便。不要说在城市街区中没有骡马粪便可捡，即便到农村地区野外去，也捡不到这样特别的骡马粪便。因为这一堆粪中夹杂许多一、两寸长的草茎，它们明显是经铡切而成的饲草。饲草是在马厩里分发给骡马食用的，只有厩肥才会夹杂这些东西。野外行进中的骡马所排放的粪便不可能夹杂这种饲草。因此可以肯定，雷锋脚下的这一堆粪肥是从马厩里起出来的，而不是从野外捡来的。这张照片应该是由雷锋季增等人先从马厩里挖出粪肥堆成一堆，再从别处弄来几砣人粪放在上面做样子，然后摆姿势拍下来的。雷锋们弄虚作假糊弄大众，也太公然太放肆了点。<br />
<br />
<br />
&nbsp;&nbsp;照片中的那几砣“招牌”大粪也很可疑。虽然它们看上去的确象人粪，但并不象雷锋所说的那样属于“小孩”粪便。小孩没有那么大的肛门。现在不清楚雷锋们是从哪里搞来的这几砣大粪。不管是在营口还是在抚顺望花区，雷锋们要在驻地周围街区里找到几砣成年人遗弃的粪便决不是件简单的事。至少不是随时随地想捡立刻就能捡到的。我想他们未必愿意花大半天时间无目的地到处找。倒不如自己生产一点更便当些。因此，我们不能排除雷锋、季增等人自产自用道具大粪的可能性。<br />
