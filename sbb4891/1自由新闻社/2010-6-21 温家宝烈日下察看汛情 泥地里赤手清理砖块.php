<br />
<img src="http://img1.gtimg.com/news/pics/hv1/105/127/552/35926290.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
6月20日，在苍梧县沙头镇双尚村小学操场受灾群众安置点，温家宝总理走进村民林保兰居住的临时板房内，握着她的手安慰她节哀保重。70多岁的林保兰在灾害中失去了3位亲人。<br />
<br />
总理19日至20日实地察看广西梧州汛情<br />
<br />
　　温家宝泥地里赤手清理砖<br />
<br />
刚刚经历百年一遇特大干旱的广西，一些地方旱情尚未完全解除，就发生了较为严重的暴雨洪涝灾害。19日至20日，国务院总理温家宝来到广西梧州市，实地察看汛情、灾情，慰问奋战在抗洪救灾一线的干部群众，主持召开会议，部署防汛抗洪救灾工作。【天气查询】<br />
<br />
40℃烈日下察看水情<br />
<br />
据气象预报，未来几天南方部分地区将出现新一轮强降雨，更大的考验还在后面，必须有更加完善的预案，做到有备无患。 <br />
<br />
梧州地处西江干流，广西八成以上的水量经这里流入广东，历史上洪涝灾害频发，是全国重点防洪城市之一。6月17日，西江发生入汛以来全国大江大河首次超警大洪水。19日下午，温家宝一抵达梧州，就来到西江大桥上察看水情。梧州气温达近四十摄氏度。 <br />
<br />
他顶着烈日，扶着大桥栏杆极目远眺，只见江面十分开阔，水流缓涨。温家宝一边听着介绍，一边察看水情。他对当地负责人说，据气象预报，未来几天南方部分地区将出现新一轮强降雨，更大的考验还在后面，必须有更加完善的预案，做到有备无患。<br />
<br />
叮嘱居民要注意安全<br />
<br />
把防洪、防地质灾害和建设廉租房、改善群众居住条件结合起来，既解决了群众安全问题，又改善了居住条件，这样的惠民政策群众满意，我们一定要坚持下去。 <br />
<br />
梧州市山有5万多人住在山坡上，暴雨天气极易造成房屋倒塌、山体滑坡、泥石流等地质灾害。2005年汛期，温家宝曾冒雨来到山上房屋密集的平民冲考察，要求加强地质灾害综合整治。20日傍晚，温家宝来到平民冲，山谷两旁的住房大部分已经拆除。看到坡上还有几户人家，温家宝走进去看望他们。66岁的陈树材正在自家电扇下纳凉。当看到温家宝脸上挂着汗珠走进来时，老陈紧紧握住总理的手。老陈说，政府已经建好新房，过几天就要搬进去。温家宝边听边叮嘱他，现在正是汛期，没搬走前一定要注意安全。 <br />
<br />
随后，温家宝来到富民小区。这个小区已建设1500多套廉租房和经济适用房，安排了从平民冲等地搬迁来的住户。温家宝走进罗志雄家，与他聊起了家常。70多岁的罗志雄年轻时是跑船的，居无定所，后来在平民冲住下来但每年夏天又担心暴雨成灾。去年9月搬迁过来，现在四口人住着50多平方米、两室一厅的新房，罗志雄说：“如今总算踏实了，下再大的雨也能睡个好觉。”温家宝听后拉着老罗的手说，把防洪、防地质灾害和建设廉租房、改善群众居住条件结合起来，既解决了群众安全问题，又改善了居住条件，这样的惠民政策群众满意，我们一定要坚持下去。<br />
<br />
挽裤脚泥地里清理砖<br />
<br />
乡亲们的家园受到破坏，有的还失去亲人，我们对遇难者表示深切哀悼。雨还在下，洪涝灾害随时可能发生，我们要严阵以待，准备迎战，第一位的就是要保护好群众。<br />
<br />
20日上午，温家宝冒雨来到苍梧县沙头镇双尚村杉木坪。这里依山而建的一片农舍被泥石流夷为平地，解放军、武警、公安消防官兵和村民们正在清理废墟。温家宝挽起裤脚，站在泥地里，弯腰与战士们一起赤手清理废墟中的红砖。 <br />
<br />
离开杉木坪，温家宝又来到两里外的双尚村小学操场。这里临时搭起了28顶救灾帐篷，成了受灾群众临时的家。70多岁的林保兰在灾害中失去了媳妇和一个孙子、一个孙女，温家宝走进她家的临时板房里，握着她的手安慰她节哀保重，并要求当地负责人一定要把老人的生活照顾好。走出板房，温家宝说，乡亲们的家园受到破坏，有的还失去亲人，我们对遇难者表示深切哀悼。雨还在下，洪涝灾害随时可能发生，我们要严阵以待，准备迎战，第一位的就是要保护好群众。在党和政府的领导下，军民团结一致，干部群众共同努力，我们一定能够战胜灾害，把家园建设得更美好。本版文图均据新华社</div>

							
							
							
							
