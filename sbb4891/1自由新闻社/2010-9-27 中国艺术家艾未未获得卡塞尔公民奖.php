													<h2>艾未未获得卡塞尔公民奖</h2>
						
						
																											<div id="postmessage_845590" class="t_msgfont">月26日，在德国卡塞尔城市歌剧院举行了卡塞尔市公民奖-&quot;理性棱镜&quot;奖（Das Glas der Vernunft）的颁奖仪式，来自中国的艺术家艾未未获颁此奖，同时获得奖金一万欧元。 <br />
<br />
此次活动成为小城卡塞尔的城中盛事，一千多民市民出席在城市歌剧院举行的颁奖仪式。公民奖协会主席麦西尔（Hanjoerg Melchior）、卡塞尔市长贝尔特拉姆.希尔根（Bertram Hilgen），杜塞尔多夫艺术馆的主任格雷格.杨森（Dr.Gregor Jansen）、卡塞尔大学的海茵兹.布德教授（Prof.Dr.Heinz Bude）等在活动上分别致词，给予艾未未艺术造诣和公民精神高度评价的同时，也鼓励世界各国的艺术家用艺术纪录社会，给那些正在受到暴力威胁的人们以希望。&nbsp; &nbsp;&nbsp; &nbsp;<br />
<br />
这个奖属于每一个独立的、不屈服、热爱自由的人<br />
<br />
艾未未在全场观众长时间的起立鼓掌声中，举起&quot;理性棱镜&quot;奖奖杯，他说：&quot;这个奖属于每一个独立的、不屈服、热爱自由的人们，由于对这个世界的理解不同，我的很多朋友蒙受痛苦，甚至失去自由，他们也受到不公正的待遇。这个奖项也体现出今天的人们，德国人，卡塞尔人，始终在关注着我们的精神状态和生存处境。作为艺术家，我一直试图寻找更新的表达的可能。&quot;<br />
<br />
<br />
卡塞尔公民奖&quot;理智棱镜&quot;由该市市民于1990年发起。当时正值两德统一，国际关系面临重整，而这种新的关系一直与启蒙时代相联系着。所以此奖一直授予致力于启蒙、理性以及宽容的政治家、艺术家以及科学家。在历届得奖者中有政治家、学者、音乐家和哲学家，其中包括以色列前总理拉宾、音乐家鲁宾梅塔、德国的知名政治家高克。<br />
<br />
卡塞尔公民奖协会主席：艾未未首先是一位世界性的艺术家<br />
<br />
据卡塞尔公民奖协会主席麦西尔介绍，授予艾未未该奖是为表彰他在其故乡中国勇于同弊端和腐败现象作斗争。他还说，艾未未不受外部影响的真实感知使他成功扮演了东西文化间协调者的角色；他能够以批判性立场面对集权国家的体制，他的作品用艺术化的手法纪录现实，通过可视的方式表现出内在的冲突和对立，能够反映当代社会性和政治性的冲突。他的话语并不是煽动性的，而是发出挑战，对陈腐的威权社会和政治体制不做妥协。麦希尔表示自己很早就对中国有所了解，但真正认识中国的艺术和文化则是通过艾未未，继而通过艾未未又了解到中国的现实；他认为选择艾未未获奖是因为他首先是一位世界性的艺术家，而不仅仅是一个政府的批评者。<br />
<br />
艾未未接受德国记者采访<br />
艾未未和卡塞尔有深厚的缘份，他参加过2007年的卡塞尔&quot;双年文献展&quot;。卡塞尔文献展是全世界范围内的艺术家在社会文化进程中进行有效互动的平台，艾未未是第三位曾经参加卡塞尔&quot;双年文献展&quot;并获得卡塞尔公民奖的艺术家。</div>

							
							
							
							
