<br />
怎么看这件事？<br />
<br />
1、做好切割。<br />
<br />
文怀沙奸污女青年入狱，这是历史事实，也是新闻里吸引人的噱头。但是，奸污女青年并不是质疑文怀沙的理由。马丁.路德.金博士生前酷爱嫖白人娼妓，这是他的私德上的问题，和他领导的黑人民权运动没有关系。衡量金博士的个人价值，用他嫖白娼作为论据是说不过去的。同样，质疑文怀沙就应该质疑他的学术水平，而不是他的包皮水平。<br />
<br />
李辉攻击文怀沙的作风问题，是中国人斗人的一贯手法。这种手法是不及格的，降低了批评文字的力量。<br />
<br />
2、李辉的胜算<br />
<br />
文怀沙在三四年前以大师身份行走江湖的时候，其实就已经有人出来扔鞋了。但是现在李辉扔，效果要更好一些。因为他和一帮文化界和学术界的老家伙很熟悉。请看大屏幕：<br />
<br />
李辉，1982年毕业于上海复旦大学中文系，现供职《人民日报》编辑文艺副刊。主要从事传记文学和随笔创作，先后发表论著《巴金论稿》、历史纪实《胡风集团冤案始末》以及萧乾、沈从文、黄苗子、郁风等人传记等。<br />
<br />
基本上文革一结束，他就出动了。和这些老家伙们混在一起，获得了许多一手资料，这也是他为什么能做这些传记的原因。以他手中的弹药，攻击一下文怀沙不成任何问题。<br />
<br />
3、文怀沙就是一个模特<br />
<br />
近年来玩“大国崛起”，人们需要证明自己的传统文化很牛，也就迫切需要寻找一个活的大师出来站台。于是，就有了文怀沙。<br />
<br />
文怀沙号称国学大师，其实没有任何狗屁学术创建。所以，他行走天涯的口号是“述而不著”，就是自比孔子，我都是言传身教，却不写在纸面上，而是让弟子们去领悟和传播。孔子是真忙，所以没时间却写作。而文怀沙是真文盲，所以他是写不了。只不过白须飘飘，满足了群众对于大师的想象，加上本人敢于忽悠，也就因此靠相貌和年岁变成了“国学大师”。<br />
<br />
如果乌龟可以长白毛的话，等它活了一千年，大概也就是“国学大师”了。<br />
<br />
4、结论 <br />
<br />
早该扒了。</div>

							
							
							
							
