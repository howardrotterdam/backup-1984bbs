																											<div id="postmessage_209731" class="t_msgfont">亚洲周刊：维权拒绝悲情 艾未未和他的战友们<br />
<br />
<br />
张洁平<br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;八月十二日凌晨三点十七分十五秒，在成都“安逸158”酒店，艾未未的房间被一脚踹开。门上遍布伤口，是被警棍猛力捶打留下的。艾未未还没完全清醒，对涌入房间身着警服的人说了句：“你们执法要先亮证”，对方一拳就上来了。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;四天后，艾未未在北京家中说起这段经历时，加了很多象声词：“咣一脚把门踢开”、“哐一拳打过来”、“脸哗地就肿起来了”。此时他的脸已经消肿，只是还显得疲惫。五十三岁的他表情平静，像是在说一件别人的事儿。精确到秒的挨揍全过程，已经整理出来，“今天就发上网去”。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;四川之行，他本是到成都市中级人民法院为谭作人出庭作证的。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;谭作人和艾未未一样，以公民身份自发调查512遇难学生名单，如今被以“煽动颠覆国家政权”罪名起诉、并可能面临刑责；艾未未要证明，当局指控谭作人的地震调查“无中生有、捏造消息”，或者“诋毁党和政府形象”都不符事实。因为，在他们不约而同的、分别进行的、长达数月的实地调查中，超过五千名孩子的死亡是真实的，孩子家长的苦痛是真实的，对校舍质量的质疑和愤怒是真实的，学生死亡与豆腐渣校舍之间的关系也是真实的。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;不过，刚到达成都的艾未未一行，还未及出庭，就经历了开头那一幕。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;七八个房间同时响起的巨大捶门声刺穿整栋楼，四五十个警察包围了睡梦中的他们。那阵仗，仿佛缉毒或者追凶。此后，他和同行的志愿者们在酒店被困了十一个小时，一直到八月十二日下午两点半，才重获自由。这一段时间内，距离酒店五百米外的成都市中级人民法院，谭作人案开庭与审结的整个“表演”已经完成。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;谭作人在被关押138天之后，第一次开庭，控辩双方却没有一位证人到场。辩方律师浦志强和夏霖所邀请的三位证人：地质学者范晓与环境学者艾南山被挡在法庭门口，艺术家艾未未，则肿着脸被困在宾馆。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;被困的十一个小时里，艾未未要求去了医院，与围住他们的警察无休止地争执，间中，用手机发出上百条Twitter信息，接受了20多家媒体，包括7、8家香港媒体的采访。“他们想把电话抢走，我要回来了。”<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;而且，他的房间里放了录音笔，从响彻大楼的捶门声开始，记录下了他与身着警服的执法人员之间每一句对谈，包括关于出示执法证件的盘问、关于打人了还是没打的争吵，“否则打完了之后他们会说谁打人了，谁看见打人了，他们真是很无聊，但我也得争啊，不能不争啊。你去听听，比任何话剧都好看。”<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;在此前由他发起，近百名志愿者参与的512公民调查中，这已经是许多人熟悉的艾氏风格：恪守理据的同时姿态强悍，精神气场强大，即使身处弱势，也绝非弱者；这样一来，对手看似强势的蛮横、暴力反到成了露怯的表现。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;艺术家身份赋予了这位公民维权者特别的气质，这种精神气质与互联网时代精神相应和，正在中国蹒跚前行的维权运动中，带来一种微妙的变化。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;与以往背负沉重枷锁的悲情英雄不同，今天的维权者姿态更阳光、气质更年轻、表达更新鲜、心态更平和，这种弱势而非弱者、深情而非悲情的精神气质，让“维权”的样子不再是低姿态的争取，而是平等的诉求，甚至成为更日常的公共生活的一部分。尽管，严酷的代价从未减少，可是，在这样自信而具有平常心的精神强者面前，他们的对手却从未像现在这样显得懦弱，而且狼狈不堪。<br />
<br />
&nbsp; &nbsp; 艾未未如是，谭作人如是，刚刚被正式批捕的公盟许志永也如是。<br />
<br />
&nbsp; &nbsp; 在浦志强律师的相机里，记者见到狱中谭作人的样子，短发，没有胡须，面容稍显苍白，但不算消瘦，镜头前总是在笑，气色看来很健康。与印象中总是黑衣黑帽、神情沉郁的那个孤身上路的男人很不同，也与网友印制声援T恤上，那张长发大胡子的侠客照片不同，隔着铁窗的谭作人，看来更坚持也更淡定。<br />
<br />
&nbsp; &nbsp; 第一次见到谭作人的浦志强一个劲称赞他：“汉子，真是条汉子！”<br />
<br />
&nbsp; &nbsp; 在成都市人民检察院七月三十一日的起诉书中，谭作人的罪名是“涉嫌煽动颠覆国家政权罪”，罪状则主要有四条：撰写《1989：见证最后的美丽――一个目击者的广场日记》；与王丹联系；零八年六月四日当天在成都义务献血；512地震后“多次接受境外媒体采访”，“发表大量严重诋毁我党和政府形象的言论”。<br />
<br />
&nbsp; &nbsp; 浦志强表示，在与律师的两次会面中，谭作人清楚地表达了自己的态度：绝不推卸自己应该承担的责任，也不承担本不是他的责任。浦志强说：“谭作人说，凡是他说过的话，他全认。他在整个事件中不牵扯任何人，不出卖任何人，非常敢作敢当。另外他说，如果二十年后历史选择他来为当年June Fourth的事情承担责任，他愿意把牢底坐穿；如果地震公民调查还家长和孩子一个公道，这件事情如果要负责，要判刑，他认了。” 浦志强感叹，老谭心态非常平和、达观。<br />
<br />
&nbsp; &nbsp; 去年十一月，亚洲周刊曾因为彭州石化环保事件采访谭作人，作为一个身处弱势的普通公民，他不卑、不亢、不抛弃、不放弃的姿态给记者留下深刻印象。他反对在地震断裂带上建立巨型石化工厂，他与政府沟通，写公民建议书，未果；他与国保、公安部门沟通，申请散步，未果；于是，他发起最最低调的“和平保城”，呼吁成都人上街带一张A4白纸，或者白面具、白帽子、白口罩、白胸花……以白色来表达拒绝，用他的话说：“用全体示弱来代替集体示威，用消极行为来主张积极权利。用白色表达来反对黑色操作，用有序渐进来学习民主程序。”<br />
<br />
&nbsp; &nbsp; 许多成都知识圈的好友并不认同谭作人的做法，激进者诟病他“过于温和”，也有人不明白，这样充满“行为艺术”意味的表达方式“有什么用呢”？<br />
<br />
&nbsp; &nbsp; 二零零八年十一月在成都，谭作人试图发动他那辆上百趟开进地震灾区的破吉普而未果，转身把车一点一点推到路边，让开通道的时候，你能看到的，却是他身上最平和但最坚持的东西。这么多年他的努力，从成都天府广场修隧道、一条小小柏水河的保护，到为石化工程迁址鼓呼、为地震遇难孩子呐喊，到因为香港一个不相干的政治人物马力触碰了他不能被歪曲的伤痛记忆，有感而发写下《广场日记》，他从来只是做一个公民能做的、该做的，并且把它做得漂亮，做得完整。正如艾晓明在《四川好人谭作人》中所写，他只是个好人，只会身体力行。<br />
<br />
&nbsp; &nbsp; 二零零九年七月一日，艺术家艾未未在发起“七一罢网”的时候写过一篇博客，文中他写道：“我们变成今天这样处境，是因为中国的聪明人太多，他们总是说，‘这样做有什么用呢？做了之后又怎样呢？’我突然明白了许多，在这样里的任何的抗争，要面对的不仅仅是强权和暴力，更是那些更聪明的嘲讽者，他们无处不在的表现出世故圆滑，老谋深算，趋利避害。……他们都不善于动手做事，缺少想象力和幽默感，在质疑一切之后隐藏着自暴自弃。……他们无一例外的成为了自我放逐者和自我恐吓者。”<br />
<br />
&nbsp; &nbsp; 从未见过谭作人的艾未未千里迢迢来成都挨拳头，为的正是这样一个遥远的同路人，他和他一样，有想象力和幽默感，勇敢，坚持身体力行，并且胸怀坦荡。<br />
<br />
&nbsp; &nbsp; 让谭作人很乐的事儿是最近收到的七八张明信片，上面都唤他回家吃饭。他问浦志强这是什么意思，怎么都说“妈妈喊你回家吃饭”。<br />
<br />
&nbsp; &nbsp; 七月中旬开始，一句无厘头的网络流行语被应用于声援良心犯的运动，写着“妈妈喊你回家吃饭”的明信片从世界各地雪片般飞向关押良心犯的监狱。如果说幽默是精神力量强大的表现，有幽默感的维权运动也从“明信片”开始，在互联网世界渐成氛围。<br />
<br />
&nbsp; &nbsp; 福建网友郭宝锋是被“喊”回家的第一人。<br />
<br />
&nbsp; &nbsp; 郭宝锋因在网上传播“严晓玲遭轮奸致死”的信息，7月16日被福州马尾警方以“涉嫌诽谤罪”拘留。随即，网友“北风”等人发起“一人一张明信片，喊郭宝锋回家”行动，据不完全统计，数以百计写着“郭宝峰，你妈妈喊你回家吃饭”的明信片从十数个国家和地区被寄往福州市第二看守所。7月31日，郭宝峰真的获得取保候审回了家。<br />
<br />
&nbsp; &nbsp; 紧接着，谭作人所在的成都温江区看守所和许志永所在的北京第一看守所成了漫天明信片的下一个目的地。<br />
<br />
&nbsp; &nbsp; 网友“兽兽”说：“这个夏天，互联网上一声咬喝，‘×××，你妈妈喊你回家吃饭’，仿佛全世界都是叉腰站在巷子口，声嘶力竭唤儿女归家的妈。”<br />
<br />
&nbsp; &nbsp; 明信片究竟能发挥多大的效用，众说纷纭。郭宝峰并没有亲手接到一张明信片，但见到网络上不完全版本的备份后，他感动落泪。看守所会否因此受到压力，当局能否因此感知民意――“明信片运动”的倡议人之一北风认为，这些都不是最重要，“网友寄送明信片的行动本身，就是克服内心恐惧及自我救赎的过程。”<br />
<br />
维权律师唐荆陵更指出：“‘明信片’就是‘民心片’”，他断言：“民主将从明信片开始”。四川作家冉云飞说：“他这两句话完美而淮确地表达了这一场看似行为艺术的、给狱中维权人士和良心犯寄明信片活动的意义。”<br />
<br />
&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;就在谭作人案开庭、艾未未被打的同一天，也是八月十二日，公盟的创始人、北京大学法律博士许志永因“偷税”罪名被正式逮捕。而此前，他和公盟财务人员庄璐已经被公安带走两个星期。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;这家中国内地最有影响力的法律维权组织，被税务部门指瞒报所得，需补缴税款和5倍罚款，总额近130万元人民币。七月二十九日，在许志永被带走后，网友自发发起捐款活动，希望帮助公盟补缴税款，让许志永能够回家。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;八月三日，公盟发布接受社会捐款用于旅行税务行政处罚决定的公告，积极筹备资金，但八月五日，该账户却被北京市公安局冻结。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;八月十日，公盟分别到地税和国税两个部门申请缴纳税务罚款，但都被拒绝，理由是：“没有法人代表签字，不能缴税。”<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;公盟的法人代表是许志永，他在牢里，不能签字，因此不能缴税，不能缴税，因此他不能被释放。――荒诞逻辑绕成死局，更多的人开始为许志永担心。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;朋友们说，许志永希望扮演桥梁的角色――在这个碎片化的、公民没有组织、无从表达的社会，许志永们的存在是冤屈满腹的上访者唯一可能与体制建立正常沟通的桥梁――“可惜的是，政府似乎不需要桥梁”。这样的桥梁完全切断，会是什么结果，许多学者已经提出警告，今年中国遍地开花的群体性事件已经是个糟糕的预兆。时评作者刘天昭说：“这或许证明，公民维护自己权益的其他途径已经逐渐失效；而公众大概也逐渐意识到，敦促基层政府作为，群体性事件是有效的方式。如果中国的民间维权运动进入这样的逻辑，那么想要恢复体制内的其他维权途径将会变得更加困难。”<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;网民自发成立了网站“我为志永”（<a href="https://wezhiyong.org" target="_blank">https://wezhiyong.org</a>），他们的口号是“千千万万个志永，不在监狱，就在走向监狱的路上”、“每个人都可能是许志永”。这个网站随时更新公盟案与民间支持的一切行动。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;而同时，公民捐款仍在进行，截至2009年8月13日，公盟（以北京公盟咨询有限责任公司帐号为主）共收到社会各界捐款额为人民币845,783元。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;没有人统计，究竟有多少明信片，寄到了“北京市朝阳区豆各庄501号北京第一看守所”，呼唤许志永回家。但是不断涌现的声援许志永的各种创意无限的公民行动，让许志永的同道者感到了温暖。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;比如卖T恤的深圳网友张扬（Twitter用户名为digitalboy）在自己的购物网站上贴出了通告：“Rescue X 计划，捐50元，T恤买一送一”；还有“请为公盟捐款5元，本店就优惠6元”的活动，注解：“营销游戏，得到总比付出多”。<br />
<br />
这个软件工程师、自由服装（FreeGeekTee）爱好者的T恤店主通过设计，笑眯眯地表达了立场。设计的襟章很简单时尚，只有六个字：“许志永，纯爷们”。<br />
<br />
&nbsp; &nbsp; 在北京时尚杂志《时尚先生》（Esquire简体中文版）八月刊上，许志永身着时尚衬衫，戴时下流行的窄款细领带，成为拥有“中国梦”的“时尚先生”。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;这个出生于1973年的年轻法律人比谭作人和艾未未小了近二十岁，但他们拥有相似的强者精神和阳光气息。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;在杂志中，许志永这样阐述自己的中国梦：「我希望我们是个自由幸福的国家。每个人不需要违背良心，只要靠自己的才能和品德就可以找到合适的位置；一个简单而幸福的社会，人性的善得到最大的张扬，恶得到最大的抑制；诚实、信用、友爱、互助将成我们生活的常态，没有那么多烦恼和愤怒，每一个人脸上是纯真的笑容。」<br />
<br />
&nbsp; &nbsp; 而此前的六月刊中，艾未未也以仰头望天的造型迈入“时尚先生”行列，他说：“公民社会不是什么高深的理想，就是一些基本的原则。有时实现你的理想就像出生，需要通过一个黑暗的非常困难的甚至令人窒息的隧道，才能达到光明的彼岸。”<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;谭作人和许志永正在为这样的理想通过隧道；艾未未分享他们的苦难，完成着自己的坚持。当艾未未、谭作人、许志永的身份不是艺术家、杂志主编、海淀区人大代表，而使用一个共同的新名字“公民”的时候，他们变成了这个社会的弱势者；但同时，在这个新名字背后，是潜规则如皇帝新装一样被戳穿的新世界，如艾未未所说，他们“用自己的语言消解旧的方式，用自己的态度替代成见，用新的表达覆盖传统的方式。这是新的生活因为是我们自己的方式。”<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;当许志永成为时尚先生，艾未未称自己为“八零后”，谭作人坚持自己“白色保城”的“行为艺术”；当雪片一般的明信片继续飞向黑暗的监狱，当各种坚持以幽默的方式继续，你会相信：黑暗的姿态只有一种，光明的样子却有千千万万。他们才是我们的未来。<br />
