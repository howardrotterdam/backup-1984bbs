专访：公民艺术家艾未未<br />
<br />
这社会最终会被光打开 <br />
<br />
<br />
张洁平<br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;二零零九年，艾未未经历了很多第一次。第一次以“公民”身份被大众了解，他的博客、twitter、饭否第一次被关，第一次被国保监控，第一次挨打……在此之前，他是混迹各个领域的国际著名艺术家，是“鸟巢”设计的中方顾问，是他不怎么愿意提及的诗人艾青的儿子。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;为大众熟知的转型是从零八年十二月十五日开始，艾未未和自发加入的志愿者团队成立了“5.12公民调查”小组，展开地震遇难学生名单调查。这项调查在艾未未的主持下持续近一年，共计百余人次的志愿者前往四川各个村镇实地考察，截至零九年七月二十八日，调查报告显示：“共统计地震遇难学生总名单人数为5194名，已核对确认4803名，尚未有能力确认的391名。其中165名是缺校名信息，226名是有校名但没有确认渠道。在确认的4803名单中，有2590名有原始名单依据，包括教育部门内部的遇难学生花名册、政府慰问家属名单，乡政府一帮一名单，保险公司理赔名单、家长联合签名等。其余通过志愿者实地调查获得。”<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;在中国“众所周知”的政治环境下，艾未未和他的团队信守承诺，做到了他们能做的极限。这份详细的、带有温度的长长名单，和详尽记录下的百般受阻的寻访过程，被许多人称为“奇迹”。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;有人说他太危险了，“要出事了”，他却要么笑说我家老头坐过牢，我还没坐过呢，嫉妒；要么认真地回答：“我能出什么事？谁能伤害我？作为一个人谁能伤害你？除非你自己，除非你忍辱负重、委曲求全。这是你伤害你做人的问题。”<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;在四川第一次被公安打，他说这是他人生中最好的状态。他坦承过去玩了许多年艺术，一直没找到合适的表达方式，现在终于靠近了。他喜欢八零后的年轻人韩寒，因为他“玩儿得很好，热爱自己的事业，喜欢女孩儿，爱骂谁骂谁，谁也别给我来这套，生活很有原则”， 他说韩寒像是黑暗屋子的裂缝里钻进一道光，“很确凿的，你就知道，这个社会最终有一天会被这些光打开。”<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;他说自己也是八零后，越活越年轻。“别着急，我现在是热身、起跑阶段，刚开始呢！”跑起来什么样？“肯定超出想象，任何事情如果没超出想象，我都不愿意做。”<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;艾未未的家兼工作室在京郊草场地艺术区，这里原来是他艺术团队工作的地方，现在进驻了许多年轻的512调查志愿者，完整的地震遇难学生名单贴满了整面墙。在这里，还生活着四十只流浪猫和七只流浪狗，这是艾未未生活的另一部分。与他访谈的进行中，一只蓝眼睛白色猫咪爬上桌子，枕着我的手，就这么睡着了。<br />
<br />
为什么来四川为谭作人作证？<br />
<br />
律师找我。我知道他被抓了，因为我们在做同样的工作。我觉得我和谭作人至少是应该同罪同罚的，甚至我比他做的还多，声音比他传得远。我们是一致的，道义和目标都是一致的，得出结论也是一致的。为他作证是没法推脱的事情。<br />
<br />
如果你成功到达法庭，你会说什么？<br />
<br />
我会清楚阐明学生死亡人数和豆腐渣工程之间的关系，证明谭作人的罪是不成立的。这是所有人都看到的问题。我是提前淮备好去，让我作证或者不让我作证我都会去。我淮备了十几个案卷的材料，包括我们的完整名单，包括对几千个家属的采访，有录音有录像，还有18个月的公民调查的所有日记，这日记里包含了调查的艰苦，政府的干涉，我们26个人都被抓进去过，有两个人被打，可以看出政府对这个问题的态度，就可以理解政府为什么要抓谭作人。<br />
<br />
艺术和你做这些事情的关系是什么？<br />
<br />
艺术只是个表象，但你还有肌肉，还有血脉，这是不可分的。我从来认为，任何表达都是政治的，我以前的艺术从来没有找到合适的表达方式，现在有些接近了。以前我也没这个话语权，后来玩博客玩大了。我确实是有预谋的。包括做艺术，包括在很多方面的声望，都是为了获得一个比较坚实的平台，至少说出我想说的话。所有的公众平台，最重要就是为公众说话，我肯定是看见了这个钉子才去买个榔头的，没这个钉子，我手上拿着榔头干什么？<br />
<br />
现在是你最好的状态吗？<br />
<br />
是我最好的状态，尤其那一拳打我脸上的时候是我最好的状态。那时我想起我父亲七十多年前在上海法租界，他们左翼淮备搞游行，正在开会呢，国民党一群便衣、警察冲到屋子里，别人都不争执，就他争执，跟我一样。他刚从法国回来，还比较书生意气，满脑子自由、平等、博爱的，结果叫人家臭抽了几个耳帖子。打我那下我忽然想起这事儿，还挺有意思。他被判了六年，我和他还不一样，还没被判。<br />
<br />
你愿意被称为艺术家还是公民？<br />
<br />
那当然是公民，公民更接近，艺术家是太奇怪的一件事儿了，艺术家就好像称呼一个卖臭豆腐的人是卖臭豆腐的，这有点贬低的意思。公民我也不喜欢，叫一个人是公民……就好像叫一个人是人这样，也挺奇怪的。我也老爱用“公民”这个词，但说实话我不喜欢这个词。它是一个公民社会、民主社会的概念，它的含义和参与有关，承担责任。一切的政治最后要回归到个体生命的公平、平等上，这个说起来很简单，所有人类的理想最后无非是回到这个最傻的问题上。这个词在有些地方要付出很大的代价。但是，这个词并没有谈到更多东西啊，并没谈我们的精神处境、生活质量、存在的品质，它只是要求一个合理的秩序，这个秩序不是对每个人最有利，但是对每个人最无害。<br />
<br />
你以前给人的印象是不讲秩序的，是秩序破坏者。<br />
<br />
对，我是最无秩序的，本来我认为我是无政府主义者，自由主义者，个人主义者，是反秩序的，颠覆的。但是呢，我现在又做最具体的事情，处理最细节的问题，而且很严谨。我觉得有效的秩序破坏者，必须是对秩序的最大理解的人。没有理解的破坏是没有含义的。很多人生来就愤青，我觉得那是还没看清楚怎么回事儿呢。我不太一样，我在各种环境下生活过，在压抑的极权制度下，我们家被发配到新疆二十年；后来一句英语不懂去了美国，而且是在文化层面上呆了十二年；后来回到中国，在经济变化中，看着一个国家利益集团怎么样把资源瞬间转化为个人权力的维护，怎么样让每一个人为他们的统治付出代价。<br />
<br />
被盯、被抓、被打，这样的遭遇会让你觉得受到威胁，或者感到恐惧吗？<br />
<br />
没有。我九三年回来是做了淮备的。我八一年离开中国是淮备永远不回来的，是绝望地走的。八一年星星画展，他们把魏京生判了，他是我们的朋友，一个二十多岁的青年，对这个国家最有情感的人，以特务罪被判，我觉得这个国家完全没希望了。九三年回来之前，我只问自己一个问题，如果回来被抓起来怎么办。因为我在国外参与好多民运的事儿嘛。我想最多就是坐牢嘛。我父亲唯一让我嫉妒的就是他坐过牢，我还没有。虽然快了，但是还需要一定的努力。<br />
<br />
第一次这么全面地认识中国的维稳系统有什么感受？<br />
<br />
他们太不长远了。他们就是靠强大的机器，不惜一切代价在那儿挥霍。就好比他们要打死一个蚊子，后来就说把房子拆了吧！而且越补这个洞越大。你看谭作人做那么点的事，现在搞成多大的事儿。整个系统都要来弥补。这里很多事在预期之中，但永远会超出我的预期，这国家还有没有人当家啊，你会觉得。谁也不知道别人在想什么，哪里发生了什么，这个政权是建立在这个基础上的，是蒙着你的眼睛和耳朵来打你的，偷你的，是捂着你的嘴来抢你的。这是很下流的恶行，很难改。自由、人权，这是最基本的价值，这些基本价值上没有什么可争论的。这个国家变好能变成什么样我还真不知道，只知道变坏能变到怎么坏。我希望世界变好，是能给每一个人多一点施恶的空间，不要让所有施恶的权力都集中在政权身上。<br />
<br />
你怎么看待你的志愿者团队，和像韩寒这样的八零后？<br />
韩寒有年轻的直觉，自信。直觉是说简单的善和恶的评价；自信是说非常信任生命给你的这种权利，没有受到教育的破坏。他极大运用了生命原始的力量。而在教育、知识文化和人的本性的交换之中，大多数人都是失败的。我们的志愿者也都很勇敢，超出我想象的成熟和坚定。他们要承受巨大的压力，警察要抓你，出租车司机要举报你，连网吧老板都要举报。经过几个月，他们都非常成熟。我相信一旦新闻自由了，他们立刻会转向好的一面，因为他们判断的能力还是很强的。他们的上一代人很多都放弃了，说没希望了，可他们是有判断的。<br />
<br />
专访：辩护律师浦志强、夏霖<br />
<br />
谭作人案庭审成儿戏 <br />
<br />
<br />
张洁平<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;二零零九年八月十二日，谭作人案审判长刘菡槌声刚落，夏霖就指着法官林乔痛骂起来：“你自己觉得你对得起法官这个职业吗？你把老子们的证人名单骗到手又不通知！成都国保做的臭事，你又何必顶这个雷！”<br />
<br />
&nbsp; &nbsp; 夏霖向记者复述这件事时仍然气愤难平：“在庭上要尊重法庭权威，我不说话，下庭后积聚的那些愤怒全发泄出来了！”<br />
<br />
&nbsp; &nbsp; 在拥有丰富刑事辩护经验的夏霖和著名维权律师浦志强看来，八月十二日，谭作人“涉嫌颠覆国家政权罪”的这一场庭审，包括场内场外的种种“盛况”，都是非常草率、不尊重法律、甚至很儿戏的。在场内，证人不淮出庭，律师说话被频频打断，几乎无法完整说出辩词，谭作人与妻女拥抱都未能实现；在场外，重要证人被打，十五岁的小女儿被隔离庭外，前去采访的香港记者被以“涉嫌藏毒”罪名拘押，冉云飞等几位重要关注者均在庭审期间遭遇警方强行传讯。原本一个地方案件，瞬时被升级为国际事件。“他们真有能耐”，浦志强笑说。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;亚洲周刊专访了谭案的两位辩护律师，以下是访谈摘要：<br />
<br />
关于“煽动颠覆”的罪名，控方主要列举了哪些罪状？<br />
<br />
夏霖：罪状有四个部分：第一是广场日记，第二是和王丹联系，第三是广场献血，第四是地震。我们一一做了驳回。广场日记我们查明和香港民建联马力的言论有关，谭作人的文章是因他的言论有感而发；与王丹的联系这个问题，第一，谭作人和王丹之间是私人email，不符合煽动罪的公开、向不特定的多数人的特性，第二，关于谭作人和王丹的政治关系，我当庭辩护时表达了这样一个观点：如果你要说王丹煽动谭作人，还在情理之中，你要说谭作人煽动王丹，这就有点搞笑了。王丹既然已经是境外敌人了，煽动他干什么？至于广场献血，最终事实是为灾区献血。地震的“诋毁”言论，我们淮备了最多的证据。书面辩护词我和助手夏楠刚刚完成，昨晚一点特快专递寄往成都。在辩护词末尾我引用了成都武侯祠的一句对联：“能攻心则反侧自消，自古知兵非好战；不审势即宽严皆误，后来治蜀要深思。”用在此时，此案，又是成都，我觉得非常贴切。<br />
<br />
作为辩方，你们提交了哪些证据？<br />
<br />
夏霖：当庭成堂的是两份证据都是关于彭州石化，一份是谭作人写的《关于问题工程的问题》报告，另一份是谭作人曾呈交成都市政府、人大政协的《关于成都彭州石化项目的公民意见建议书》。起诉书中没有提到彭州石化，但是我们担心他在庭上突然提起，所以做了淮备。其它十八份证据是开庭前一天下午，我们和检察官进行证据交换是提交的，地震调查的证据，包括艾晓明拍摄的纪录片《天国娃娃》视频、香港电台铿锵集视频、地震期间《南方周末》、《财经》、《中国国家地理》对豆腐渣工程以及水利工程的调查等，还有四五份证据证明谭作人是一个热心公益的环保人士，包括以前的人大提议等等。我们全部是原样举证。<br />
<br />
证人为什么没能出庭？<br />
<br />
夏霖：我们提出的三个证人都被拒绝。他们的理由是“与案子有没有关联性”，但这不符合程序，应该在当庭举证完之后，有没有关联性，法官判断，没有关联，不采纳就是了；在庭审之前，只能程序审查这个证人。比如是否年满十八岁，是否有足够表达能力，是否精神正常。结果三位证人，范晓和艾南山都在庭外，艾未未在宾馆被打。<br />
<br />
浦志强：我们原本希望用三个尽量中性又有说服力的证人，来避免法庭紧张。三个证人只要有一个进去，我们一定能把他们打得稀里哗啦。可证人都在门口。我们当庭又提出要求，他们还是断然拒绝，说证人和案子无关。简单来讲，这个庭审啊，既不想说清事实，也不想澄清法理。只要在闭庭结束，法官夹包就跑了。<br />
<br />
法庭上的指控有什么问题？辩护困难在哪里？<br />
<br />
浦志强：指控很模糊，比如你要说他诋毁政府，实施了什么行为？哪一个具体的行为诋毁了政府？是搞游行集会，还是干嘛？一打接受境外媒体采访的材料，谭作人搞了个文件夹存在自己电脑里，控方就凭这个他自己收集的和他有关的作品。我们想知道，说512地震后谭作人发表大量诋毁党和国家的言论，究竟是哪一句话？他接受采访还说了很多好事儿呢，称赞党和政府救灾及时。<br />
<br />
夏霖：而且谭作人是言论始终如一的，不仅接受境外媒体采访，也接受很多国内媒体的采访。国内媒体不知道最后有没有报道出来，是不是被宣传部门把稿子毙了，那是另当别论，但是控方单单拎出几个“敌对媒体”的访问，这是不是筛选证据？谭作人并不否认他接受采访的这些内容，但我们律师就要说了。从证据学的角度，这些东西你如果要作为证据，比如我接受亚洲周刊的采访，你认为有问题，你跟我打官司要把亚洲周刊的原件拿来，当庭出示；你接受新唐人电视台的采访，你必须拿出原始视频来，这才能作为证据使用。你们这些都不做，谭作人自己把这些材料收集放在自己电脑文档里面，你们去把它抄来然后去指控别人，哪有这种事情？他在电脑里还可能说，我接受了月球电视的访问，你们也拿来做证据吗？如此重案，如何草率至此？<br />
<br />
浦志强：这些话啊，也就是夏霖现在跟你说，我们在庭上除了辩护词，所有的话，没一句是完整的。你说了一半，他就打断你说我已经知道了，不要再说了。控方很不严谨，一直到10日还在补充证据，他们甚至把抓他的日子都搞错了，3月28日，一开始所有的文件写的都是3月27日。<br />
<br />
彭州石化和June Fourth问题在这个案件中扮演什么样的角色？<br />
浦志强：当局对此次事件的关键，第一是绝对不提石化，这牵扯地方利益；第二是防止对豆腐渣工程的追究再次抬头。谭作人自己的分析是，抓他因为彭州石化损害了利益集团，地震调查损害了四川和国家的脸面。其实谭作人和国保一年要接触几十次，非常熟悉了。我个人觉得，这件事选择他来处理是经过谨慎处理后的选择。至于June Fourth这个问题，我们为他辩护，也为我们自己辩护。这个问题，审判的不仅仅是一个谭作人。<br />
 <br />
 <br />
<br />
两岸免于恐惧的自由<br />
<br />
<br />
邱立本<br />
 <br />
<br />
人民拥有“免于恐惧的自由”（Freedom from fear），是现代社会的特色。但要争取到这种自由，却往往先要经过“没有恐惧的自由”的恐惧。<br />
<br />
在台湾风灾过后，在马英九被蓝绿媒体和国际媒体炮轰得”满头包“之际，却很少人关注到这位政治领袖，在八月十五日那天，出席一个参加者不多，媒体报道也很少的聚会。在这个追念殷海光，雷震和“自由中国”杂志的研讨会上，马英九以”中华民国总统“的身份，为国民党政权当年镇压这些自由主义者道歉，并亲自向殷海光的遗孀夏君璐表达歉意。<br />
<br />
一九六九年，殷海光逝世时，马英九才是台大法律系二年级的学生。他没上过殷老师的课，但肯定念过他的书《没有颜色的思想》和《怎样判别是非》，了解这位台湾自由主义的先驱在五六十年代《自由中国》半月刊的巨大影响力，是国民党白色恐怖统治中的异数；他推广罗素，海耶克等人的思想，甚至批评胡适在雷震被捕之后所说的“容忍比自由更重要”。马英九也许从他的同学钱永祥，学长王晓波，王杏庆（南方朔）等人口中晓得，殷海光生前的最后几年，不仅被禁止在台大开课，还时刻都在特务的监视中，而殷海光的学生李敖等，后来也因文字贾祸，关进了铁窗。<br />
<br />
但这些没有“免于恐惧的自由”的恐惧，终于被前仆后继的民主运动所粉碎。今天台湾在民主化的进程中，都平反了这一切，昔日的政治犯，成为今日被尊敬和追念的人物。他们坐过的冤狱，都可以获得国家赔偿；他们所受过的侮辱，都已被洗涮。<br />
<br />
如果今天的台湾可以，为什麽明天的中国大陆不可以。当艾未未为了谭作人案要上法庭作证前，被冲进他旅馆房间的公安打了一拳之际，他会不会想到很多年很多年之后，共产党的领袖会为这一拳向他道歉和赔偿？<br />
<br />
也许艾未未没有想到那麽远，正如当年殷海光被特务包围时，他也没有想到国民党领袖将来会向他道歉。但艾未未说他在这一刻并没有恐惧，他只是觉得自己可以踏上了父亲的生命轨迹，可以进去坐牢。他说自己一直“妒忌”父亲（诗人艾青）有坐牢的经验（他坐的是国民党的牢）。如今艾未未已为自己的维权的行动，作出了心理上的铺垫。<br />
<br />
这就是一种快乐维权的心理结构。艾未未们挥别了维权的悲情，开创了新的空间。这也和当前中国大陆社会的氛围相配合。民间的公民社会的力量澎湃，早已超越了那种哭哭啼啼，自怨自艾的心态，而是从精神的层次中上升，从历史的高度赋予新的自我期许，了解公民的责任和承担，终会创造一个拥有“免于恐惧的自由”的社会。台湾的殷海光可以，中国大陆的艾未未也可以。</div>

							
							
							
							
