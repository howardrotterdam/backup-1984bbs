													<h2>哈哈，转下这个，楼上各位看看自己的逻辑谬误在那里。：）</h2>
						
						
																											<div id="postmessage_70482" class="t_msgfont">常见逻辑谬误<br />
　　说明： <br />
　　 本文主要介绍自己或者他人写作中可能发生的常见逻辑谬误，每一谬误均给出了相应定义、示例，以及如何防止这些谬误的建议。 <br />
　　 <br />
　　关于论证 <br />
　　 学术写作大都要求进行论证，即：对要提出或者阐释的见解，应给出理据。也许你得到了这样的评语：你的论证逻辑不够严密，也不够有力，而你又担心自己在逻辑性方面较弱，或者不清楚论证要有力到底什么意思。虽说学会出色的论证是一个发展的过程，然而却并非不可能做到：“讲求逻辑”任何人都能做到，多加实践即可！所有论证都包括一些“论据”（即给出理由或证据的语句），按照恰当的顺序组织到一起，从而能够支撑“结论”（即所论证的核心观点或者阐释）。要确保论证有力，应该做到： <br />
　　 1． 论据质量要高（确有把握认定论据的理据充分，因此论据不但真实而且与论题相关）； <br />
　　 2． 确保论据能够支撑结论（不能是其他结论，甚或完全不能得出任何结论）； <br />
　　 3． 检查是否已经讨论了问题的最重要或者最相关的那些方面（亦即论据与结论的焦点均着落在所论证的问题的真正重要部分）； <br />
　　 4． 论点不要过大过泛，以免超出自己的论证能力。 <br />
　　 此外，还应确保要点的陈述要连贯有序，以便读者跟上思路。这方面请参考我们关于论证及其谋篇布局的相关建议，提高自己的论证水平。 <br />
　　 <br />
　　 本文介绍了一些论证中往往造成背离上述几条的问题形式；这些问题即是“逻辑谬误”。如果觉得自己的论证有些问题，不妨看看其中是否存在逻辑谬误！ <br />
　　 若对论题抱有强烈情感，则最易陷入逻辑谬误――如果觉得结论显而易见，就更容易认定其为真，从而对相关证据不够仔细小心。为了便于了解人们如何出现此类常见错误，作为示例本文选取了许多政治论题――例如堕胎、枪支管制、死刑、同性恋婚姻、安乐死、以及色情等等问题。不过，本文的目的并非要论证任何这些问题的任何一种观点立场，而是要籍此阐明什么叫做推理有欠缜密，而这一问题可能发生在几乎任何论证之中！请注意：这些示例中的观点，均是为了说明问题而编拟出来的，并未经过科学论证，因此不应在自己的文章中采用它们作为证据。 <br />
　　 <br />
　　逻辑谬误的概念 <br />
　　 逻辑谬误是削弱论证的那些缺陷。学会在自己以及他人的文章中找出逻辑谬误，可以提高对自己的、读到的、听到的论证的评判能力。关于逻辑谬误，重要的是应该认清两点：首先，存在逻辑谬误的论证实在极为常见，且可能显得非常令人信服，至少对于那些诱因读者或者听众是如此；在报纸、广告及其他信息源中，都可以找到众多存在逻辑谬误的实例。其次，有时很难判断某论证是否存在逻辑谬误；某一论证实际上可能非常无力、有些无力、有些有力，或者非常有力；包含若干步骤或者若干部分的论证，可能其中某些章节很有力，而另外一些则很无力。因此，本文的目标不是教人如何给论证贴上逻辑谬误或者逻辑严密的标签，而是教人如何审查自己的论证，然后使其从“无力”变为“有力”。 <br />
　　 <br />
　　逻辑谬误的判别 <br />
　　 以下介绍的每一逻辑谬误，都分别给出了它的定义或解说、示例，以及论证中可以如何避免的建议。 <br />
　　 <br />
　　 <br />
　　以偏概全 <br />
　　 定义：依据不充分的例证（通常不具代表性或者过于琐碎）得出普遍的结论。诸如“大学男生联谊会的人都是醉鬼”以及“大学生都很书呆子气”等等成见，就是“以偏概全”的很好例子。 <br />
　　 示例：“我的舍友说她的哲学课很难，我的哲学课也很难，因此，所有的哲学课必定都很难！”本例的依据仅为两个人的感受，是不足以得出这样的结论的。 <br />
　　 建议：检查所选用的例证：是否只有不多几个人的观点或者经历？如是，则应考虑是否需要增加证据，抑或缩小结论涵盖范围。（注意：本例结论若改为不那么绝对的“某些哲学课对某些学生来说是很难的”，则不属于“以偏概全”。） <br />
　　 <br />
　　推不出 <br />
　　 定义：给出的论据的确能够支撑某种结论，然而却非欲论证的结论。 <br />
　　 示例：“惩罚的严厉程度应当与违法的严重程度相吻合。现如今，对酒后驾车的惩罚也许不过只是罚款。然而酒后驾车乃是非常严重的违法，可以导致无辜行人的丧命。因此，酒后驾车应当适用死刑。”这里的论据实际上可以支撑若干结论，例如“对酒后驾车应当严惩”等等，但是就本例而言，却不能支持适用死刑的正当性。 <br />
　　 建议：分清论据与结论。检查论据，看它们可以客观地给出什么结论。检查结论，看它需要什么论据作为支撑，然后检查实际是否给出了这样的论据。“推不出”的逻辑谬误往往发生在结论过于宽泛或者过于绝对之时，因此，如果所提主张较大，则应特别小心仔细。 <br />
　　 <br />
　　事后归因（亦称“假性因果”） <br />
　　 这一谬误的名称来自拉丁语“post hoc, ergo propter hoc”，意为“后此，故因此”。 <br />
　　 定义：认为事件甲之后发生了事件乙，因此事件甲导致了事件乙。当然，有时的确是前一事件导致了后一事件，例如，我选修了某门课，之后我的名字便出现在该课学生名单之中，因此确实是前者导致了后者。但是，有时时间上似乎相互关联的两件事，实质上并不存在因果关系。换言之，相互关联并不等于存在因果关系。 <br />
　　 示例：“琼斯总统提高了税收，之后暴力犯罪上升。因此，琼斯总统对犯罪上升负有责任。”税收增加可能是也可能不是犯罪上升的一个因素，但本例并未揭示二者存在因果关系。 <br />
　　 建议：要避免“事后归因”，本例的论证就需要给出一定的解释，阐明税收增加如何最终就会引起犯罪上升。因此，避免这一谬误的办法就是：如果要说甲导致乙，就还应说明甲如何导致乙，而不能仅仅说是因为甲先发生乙则后发生！ <br />
　　 <br />
　　滑坡谬误 <br />
　　 定义：声称某事之后将会发生一连串通常是可怕的后果，但却并无充分证据支撑该推论。这样的推论断定，如果再往前一步踏上“滑坡”，就必定会一路滑跌到沟底，亦即假定我们不可能中途停住。 <br />
　　 示例：“动物实验有损对生命的尊重。如果不尊重生命，即可能越来越容忍诸如战争及杀人等等暴力行为。那么，社会将很快就会沦为战场，人人都会时刻担忧自己的生命。这将是文明的末日。为了防止出现这种可怕结果，应当立即宣布动物实验为非法。”由于动物实验为合法存在已有相当时日，而文明也并未面临末日，因此似乎非常明显：这一系列事件未必就会件件发生。即使认同动物实验的确危及对生命的尊重，而不尊重生命可以导致容忍暴力，但到此也可能就到了滑坡事件的终止点：我们也许不会非得一路滑跌到文明的末日。因此，并没有充分的理由让我们接受“必须宣布动物实验为非法”的结论。 <br />
　　 与“事后归因”类似，“滑坡”谬论迷惑性也很强，难以识别，因为有时的确可以预知某事之后的一系列连锁反应。请看一个似乎并无谬误的例子：“如果我英语101课程不合格，就不能毕业。如果不能毕业，可能就找不到好工作，因此明年我也就很可能只能打零工或者做汉堡了。” <br />
　　 建议：检查论证中属于“如果甲，那么乙，而如果乙，则丙”之类的连锁引申，确保事件系列关联合理。<br />
　　 <br />
　　类比失当 <br />
　　 定义：很多论证需要类比两种或更多事物、观点或情形。如果所比较的两件事物就所讨论的问题而言实际上并非真正类似，这种类比就不恰当，基于其上的论证也就存在“类比失当”的逻辑谬误。 <br />
　　 示例：“枪支和铁锤一样，都是具有金属构件的可以杀人的工具，但是限制购买铁锤是很荒唐的，因此限制购买枪支也同样荒唐。”的确，枪支和铁锤都有一些共同特性，但是这些特性（均有金属构件、同为工具、都可能用于暴力）在决定是否需要限制购买枪支时并非属于要害问题。实际上，限制枪支乃是因为枪支很容易用于远距离大规模杀人。这一特性铁锤则没有，用铁锤击杀一大群人，恐怕很不容易。因此，这样的类比是不恰当的，而据此给出的论证也同样不恰当。 <br />
　　 只要动动脑筋，就可以在世间几乎任何两个事物之间进行某种类比：“我的作文像个泥潭，遇雨都会膨胀扩大（闷在屋里出不去时学习的时候就更多），都让人郁闷。”因此，单凭类比了两个事物这一点，其本身证明不了多少问题。 <br />
　　 在关于堕胎问题的争论之中，就常常借助类比：胎儿经常与成人相提并论，然后便争论说：某一医学手段如果侵犯了成人权利，那么同样也侵犯了胎儿的权利。诸如此类的观点是否成立，要看相关类比是否有力：成人和胎儿具有那些赋予成人权利的相同的属性吗？如果关键属性是拥有人类基因密码或者拥有度过丰富人生的可能，那么成人和胎儿的确都拥有这样的属性，上述观点及其类比也将是有力的；但如果关键属性是自我意识、理性或者能够自立生存，那么成人和胎儿就不是都具有这种属性，而相关类比也就是不恰当的。 <br />
　　 建议：明确对于命题来说哪些才是重要的属性，再看所比较的两个事物是否都具备这些关键属性。 <br />
　　 <br />
　　诉诸权威 <br />
　　 定义：为使论证更为有力，我们往往举出享有盛誉或权威的资料作为参照，介绍他们对所探讨问题的立场观点。然而，如果只是举出某个人物的大名，或者诉诸某一号称权威但却难说是名副其实的专家，就期望能够影响读者，那就犯下了“诉诸权威”的逻辑谬误。 <br />
　　 示例：“我们应当废弃死刑。众多的知名人士，例如影星Guy Handsome都曾公开表示反对死刑。”虽说Guy Handsome在演艺方面也许是个权威人士，却没有什么特别的理由认为他在政治方面的见解也应能打动人心――也许，就死刑问题而言，他若可称为权威，那么文章作者同样可以称为权威。 <br />
　　 建议：要避免“诉诸权威”的谬误，有两个很简单的办法：第一，确保所援引的权威在所讨论的话题方面确实是专家；第二，不要只是说“Authority博士认同某某观点，因此我们也应该认同”之类，还应该充分阐述该权威所依据的理由或者论据。这样的话，读者获得的就不仅仅只是某人的声名。此外，还可以选择那些公认比较中立或理性的权威，而非那些可能会被指立场偏颇的权威。 <br />
　　 <br />
　　诉诸公众 <br />
　　 定义：这一谬误的拉丁文意为“挟众”。“诉诸公众”有若干形式，但共同之处是利用期望受到欢迎以及符合主流的这种大多数人的心理，从而赢得受众的支持。其最为常见的一种形式是“从众谬误”，即试图说服受众采取某一行动或接受某一说法，只因为（据说）大家都这样。 <br />
　　 示例：“同性恋婚姻很不道德。70%的美国人都这样认为！”虽然在决定需要对什么问题立法方面，大多数美国的意见是有关系的，但是他们的意见却无疑不能决定某事是否符合道义。曾几何时，相当数量的美国人都赞成种族隔离，但是这些人的意见却不能成为种族隔离符合道义的论据。这样的论证，是在诉诸我们期望与其他美国人协调一致的心理，从而认可其结论。 <br />
　　 建议：确保没有要求读者因为其他所有人都相信、所有冷静的人都相信、相信就会受到人们的欢迎，如此等等，所以他也应该相信你的结论。谨记：流行的观点并非总是正确！ <br />
　　 <br />
　　人身攻击及“你也一样” <br />
　　 定义：“人身攻击”及“你也一样”类似“诉诸权威”，也是将人们的注意引向人身而非论证或者论据。这两类谬误的结论通常都是“不应相信某某人的看法”之类，而不能相信的原因则要么是因为某某乃是一个坏人（人身攻击）或者伪君子（你也一样）。涉嫌人身攻击的论证，针对的乃是对手本人而非对手的论证。 <br />
　　 示例：“Andrea Dworkin出版了若干专著，认为色情会伤害女性。但是，Dworkin是个丑陋的怨妇，因此不值得相信。”这里对Dworkin的长相和性格的刻薄描述，与她的论证是否有力毫无关系，因此，依此为论据乃是逻辑谬误。 <br />
　　 “你也一样”谬误是指责对手实际上也做了他自己所反对的事情，因此他的论证不听也罢。试举一例：假定父母对你讲明了不能吸烟的道理，列举了很多非常好的理由，例如有损健康、费钱等等，而你则回答说：“你说的我不能接受，因为你在我这个年龄也抽烟。你也是这样！”但是，即使父母在过去也做过他们现在反对的事情，这一事实也无损于他们的论据（吸烟有害健康而且费钱），因此，你的回答存在逻辑谬误。<br />
　　 建议：务必专注于对方的论证，而非对方的品格。（当然，如果的确是在讨论某人的品格，则应例外：如果你认为“克林顿总统不值得信任”，那么证明他不值得信任的言行的那些论据就是与论题相关的，不属于逻辑谬误。） <br />
　　 <br />
　　诉诸怜悯 <br />
　　 定义：“诉诸怜悯”是指激起人们的恻隐之心，从而接受自己的结论。 <br />
　　 示例：“我知道考试是依据答题结果给分的，但是我应该得A，因为我的小猫病了，我的车抛了锚，我又得了感冒，所以我的学习是很艰难的！”这里的结论是“我应该得A”，但是“得A”的标准衡量的是课程学习情况以及知识运用情况。这里要我们接受的理由（挺过了艰难一星期的人应该得A）显然是不可接受的。所给出的那些情况也许让人觉得相关，甚至觉得其结论值得考虑；但是，这些情况在逻辑上确实并不相关，因此，其论证也是存在谬误的。再举一例：“向企业征税是不对的，别忘了他们的慈善捐款以及他们已经付出的生产成本有多少！” <br />
　　 建议：确保没有仅仅只是激起人们的恻隐之心，从而认同自己的观点。 <br />
　　 <br />
　　诉诸无知 <br />
　　 定义：这一谬误基本上等于说：“瞧，手头尚无这一问题的结论性证据，因此，应该接受我对这一问题的结论。” <br />
　　 示例：“若干世纪以来，人们都一直在努力证明上帝的存在，但迄今尚无人能够证明。因此，上帝并不存在。”与此观点相反但存在同样逻辑谬误的说法则是：“多年来，人们一直试图证明上帝并不存在，但迄今尚无人能够证明。因此，上帝确实存在。”这两种论证都是利用证据不足来支撑结论的正确性。不过，在有一种情形中这样做，却不属于逻辑谬误：如果训练有素的科研人员通过精心设计的方法长期探求某一目标而最终未能发现，而按理又应该能够发现，那么，“实际未能发现”这一事实就可以构成“该事物并不存在”的某种证据。 <br />
　　 建议：仔细检查是否存在已经指出证据不足而又依据证据不足得出了某种结论的那些部分。 <br />
　　 <br />
　　草人谬误 <br />
　　 定义：增加论辩力量的方法之一是预测可能的驳论然后先发制人。而“草人”谬误则是先把对方的观点变成一个容易推翻的版本，然后将其驳倒而得分；但是，这如同一拳击倒一个草人或者吓唬小鸟的假人，影响实在有限，因此，击败对手已遭扭曲的观点，影响也同样十分有限。 <br />
　　 示例：“女权主义要求全面禁止色情作品，而且，凡阅读色情作品者都应受到处罚！但是，这样严厉的举措无疑是不稳妥的。因此，女权主义是错误的，不应干涉色情作品及其读者。”这里，女权主义的观点经夸大而遭削弱――实际上，多数女权主义者并不主张完全“禁止”色情作品或者惩罚仅仅阅读的人；女权主义主张的更多乃是限制诸如儿童色情这些东西，或者是主张受到色情作品伤害的人士为索赔而起诉相关出版商或者出品人，并非起诉读者。因此，这种论证并不能真正得分，其中存在逻辑谬误。 <br />
　　 建议：面对对手要宽容厚道。陈述对方的论辩时应该尽量做到同样有力、准确，并且能够将心比心。如果能够驳倒对方观点最为有力的某种辩驳，则属真正有所成就。 <br />
　　 <br />
　　转移论题 <br />
　　 定义：这是指在论证过程中偏离正题，转向某一次要问题，从而转移人们对要害问题的注意力；之后的论证往往再不会回归原题。 <br />
　　 示例：“考试分析采用曲线形式最为公平。无论如何，师生关系融洽的话，教学效果就更好。”不妨将这里的论据及其结论各自单列出来，看看其中存在什么问题： <br />
　　 论据：师生关系融洽的话，教学效果就更好。 <br />
　　 结论：考试分析采用曲线形式最为公平。 <br />
　　 这样就看得很清楚：论证偏离了正题――能够使人融洽相处，并不一定就更公平；公平与正义有时需要我们做出某些将会导致矛盾冲突的事情。但是，人们可能觉得，诸如师生关系融洽之类的问题确实很重要，因而就容易忽视这一点：上述论证并未给出能够支撑“曲线更公平”的任何证据。 <br />
　　 建议：借用提纲形式，把论据与相应结论分列出来，看看论证中提出了多少问题，各个论据能否支撑相应结论。 <br />
　　 <br />
　　虚假两分 <br />
　　 定义：“虚假两分”指设法让人认为只有两种选择，然后排除其中之一，于是便剩下似乎是唯一的答案，亦即论证想要的首选答案。但是，实际上往往存在很多其他选择，而不仅只有两个；如果我们一一都考查一下，也许就不会那么快就选择论证所想要的那一个了。 <br />
　　 示例：“Caldwell Hall大楼状况很遭，我们要么把它拆掉重建，要么就只能继续拿学生的人身安全冒险。显然，不应拿任何人的安全冒险，那么就必须把它拆掉。”这里的问题在于没有指出还有这种可能：我们可以维修该建筑，或者采取某种措施保证学生安全；例如，如果只有若干教室破损，那么我们也许可以不给这些教室排课。 <br />
　　 建议：注意检查在声称只能二者选一的时候，是否真的如此，是否还有其他选择没有提到；如果有，就不应略去不谈，而应该也给出排除的理由。同理，让人认为只存在三个、四个等等选择这样的谬误，虽然还没有正式的术语以名之，但是如果实际存在的选择不止那些，则与“虚假两分”性质相同，也应避免。 <br />
　　 <br />
　　窃取论题 <br />
　　 定义：这一谬误颇为复杂，表现为多种形式，较之此前讨论过的谬误更难判别。但基本可以这样认为：“窃取论题”就是要求读者径直接受结论而并不给出任何真正的证据；论证要么是建立在与结论内容相同的论据之上（即所谓“循环论证”），要么就是略去了论证以为依据的某一重要（然而可疑的）假定。有时，人们会把“窃取论题”用作一种常见的评语，泛指结论缺乏充分理据的那些情形，但这不是我们这里所说的含义。 <br />
　　 示例：“主动型安乐死在道义上是可以接受的，协助他人籍由死亡而摆脱痛苦乃是正当的，符合伦理的。”试将这一论证的论据及结论分列如下： <br />
　　 论据：协助他人籍由死亡而摆脱痛苦乃是正当的，符合伦理的。 <br />
　　 结论：主动型安乐死在道义上是可以接受的。 <br />
　　 把这里的论据稍加变换，就能看出同一个意思重复了两遍：“正当的，符合伦理的”很大程度上也就是“在道义上是可以接受的”，而“协助他人籍由死亡而摆脱痛苦”意思就是“主动型安乐死”。因此，该论据实际上在说：“主动型安乐死在道义上是可以接受的”，而这与相应结论完全一样！对于为什么安乐死可以接受，该论证并未给出任何真正的理由，反而难免让人质疑：“呃，那么，你为什么认为安乐死可以接受呢？”。这里的论证“窃取”（亦即规避）了真正的问题。 <br />
　　 再举一例，其中论证所需但却含糊的论据完全被略去：“杀人在道义上是错误的。因此，主动型安乐死在道义上是错误的。”这里略去了论据“主动型安乐死是杀人”，而这一论据又是大可争议的――这样的论证同样是只字不提相关论据从而“窃取”了或者“规避”了主动型安乐死是否属于杀人这一问题，意在让人们只注意到没有争议的论据“杀人在道义上是错误的”，而看不到相关的假定。 <br />
　　 建议：避免“窃取论题”的方法之一是把自己的论据及其结论按提纲形式一一列举出来，检查其中是否缺少某些环节、步骤，从而造成论据之间或者论据与结论之间的脱节；补上缺失的那些命题。如果这些命题存在争议，而又被含混敷衍过去，那就可能发生了“窃取论题”谬误。之后，检查一下论据所表达实质内容（但说法不同）是否与结论相同；如果是，那么就属于“窃取论题”。要旨在于：务必不要把试图论证的问题用作假定或者无可争议的证据。 <br />
　　 <br />
　　词语歧义 <br />
　　 定义：即在论证中某一重要词语的两个或更多含义之间游移不定。 <br />
　　 示例：“向慈善组织捐助是正确的，因此，慈善组织向我们要钱也是正确的。”这里“正确”（right）一词有歧义，既可以指某事是对的或者好的（例如“这次测验我的答案都正确”），也可以指某人对某物拥有权利（例如“人人都有生命权”）。有些论证会有意偷换词语含义，常见的此类词语有“自由”、“正义”、“正确”等等；有些词语歧义问题则属于差错或者误解。但是，无论属于哪种情况，重要的是应保证在论证中所使用的主要词语的含义始终一致。 <br />
　　 建议：找出论证中的重点词语，检查它们是否可能造成歧义，如是，则应确保没有同时使用其不同含义。 <br />
　　 <br />
　　 <br />
　　 <br />
　　 <br />
　　谬误的排查 <br />
　　 以下是查找论证中逻辑谬误的几个方法： <br />
　　 站在反对相关结论的角度，反观论证中哪些部分比较可疑，哪些部分最为薄弱，然后重点加强这些部分。 <br />
　　 列出论证的各个要点，在其下方分别列出相应的证据，这样也许就会发现某个提法并无过硬证据，或者可以更为严格地审查所采用的证据。 <br />
　　 了解自己特别容易发生哪些逻辑谬误，检查自己的文章中是否出现这些谬误。有些人频频“诉诸权威”，有些人则更容易“类比失当”或者发生“草人”谬误。重读一下自己以前的文章，看看是否存在今后需要注意防范的某种逻辑谬误。 <br />
　　 注意宽泛的说法，它们较之有所限制的说法需要更多的证据。说法中若有涵盖全部的词语如“所有”、“非”、“无”、“每个”、“向来”、“从不”、“无人”、“人人”之类，虽然有时是合适的，但较之不那么绝对的词语如“有些”、“很多”、“不多”、“有时”、“通常”等等，需要给出多得多的证据。 <br />
　　 检查并复核涉及他人品格的用语，尤其是涉及对手品格的用语，确保这些用语准确、得当。 <br />
