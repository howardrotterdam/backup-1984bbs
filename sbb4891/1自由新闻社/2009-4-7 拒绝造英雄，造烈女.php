													<h2>拒绝造英雄，造烈女</h2>
						
						
																											<div id="postmessage_70063" class="t_msgfont">作者：沈睿<br />
<br />
我是一个非常怀疑英雄的人，对人们推崇备至的英雄，我常不说话，保持沉默。因为自己是一个彻头彻尾的批判性思维教育的产物：我非常苛刻地怀疑英雄的动机。每当英雄们谈起自己的高尚目标，我就暗暗地想，这样的高谈可能在掩盖其他的无法告人的想法。我不太相信任何英雄的表白诸如我是为了历史，为了人民，为了革命，为了民主等等。每当别的人推崇英雄，赞美英雄，我也不说话，保持沉默，脑子里的问号推来搡去。<br />
<br />
我宁愿把每一个人们认为是英雄的人还原成一个人。有的人勇敢，有的人懦弱，有的人喜欢众星捧月，有的人喜欢独来独往。这样的性格当遇到事情的时候，表现出不同的反应。性格顽强的，如秋瑾、林昭、张志新等就宁死不屈，他们的勇敢和不屈服使她们为一种理念献出了自己的生命。性格不那么激烈的大多数人就屈服了，毕竟生命太可贵了，死亡是一个让我们都止步的起点。我们在某种意义上必须苟延残喘。理由是留得青山在，不怕没柴烧。<br />
<br />
让我非常疑惑的是为什么二十世纪的中国英雄大都是女的。<br />
<br />
秋瑾1907年夏天被砍头，今天的研究已经表明，她可以逃跑，甚至当时的政府希望她逃跑，这样他们就不必逮捕她了。可是她拒绝逃跑，她宁愿死。她早就想当烈士献身了。这几个月来我一直在阅读她的全集，越读，我越觉得悲哀。这是一个没有自己思想的女人，她的一切都是被塑造的，她的强烈的要当人人倾慕的英雄的思想是同时代知识分子，特别是男性知识分子话语的影响。梁启超号召人们当英雄，亲笔写作传记文章《近世第一女杰罗兰夫人传》号召人们作罗兰夫人一样的革命者，写小说《东欧女豪杰》宣传俄国革命者苏菲亚。柳亚子写文回忆当年“作人要做马志尼，娶妻要娶苏菲亚”。可见二十世纪初的人们渴望英雄到了婚姻嫁娶都是英雄的地步。秋瑾在这个环境里学习新的思想，自然深受影响，她决心成为一个英雄：“画工须画云中龙，为人须为人中雄。”她在给哥哥的信中说：“他日于书记中留一名，则平生愿足矣。”“妹近儿女诸情，俱无牵挂，所经意者身后万世名耳。”她关心的是名声。为青史留名，秋瑾送去自己的生命。她成了英雄。一百年来，她的英名越来越高，成为为中国自由解放而献身的最著名的人物之一。<br />
<br />
不久前我在一个晚餐上听人们动情地谈论林昭。他们坚持林昭就是英雄，并对林昭的妹妹坚持不承认林昭是英雄感到不解和愤怒，认为林昭的妹妹不理解林昭的英雄意义。我坐在那里，一言没发，心中却全是对林昭妹妹的理解。我不认为林昭是英雄，林昭做的可能是她基督教的圣徒信念的产物，也可能有别的原因，把林昭说成是英雄，是为了浇自己心中反对极权制度的块垒，与林昭本人没有关系。前两天和某个哈佛的博士候选人谈到林昭。她说她看了林昭狱中写的东西，很多都在想象将来的时代人们会怎样把她看成是英雄。有意思的是林昭一直设想和想象自己被记住，被看成是英雄，是这种对未来的想象激励林昭不停地写血书。<br />
<br />
秋瑾和林昭在这点上很相像。她们渴望成为英雄，并用自己的鲜血写出了自己英雄的故事。我觉得她们很了不起，她们都可以说是如愿以偿，后代人做了她们希望后代人做的事情。但是我却不愿意加入造英雄的大合唱，也不想过于歌唱她们，因为很多构造英雄的目的是为了表达拉大旗做虎皮，让自己显得强大，或自己沽名钓誉。更重要的是，自从我知道刘胡兰和雷锋都是假英雄之后，我就不相信任何英雄，即使那些英雄是反对集权和极权的。我不愿意参与造英雄，用英雄的榜样贻误他人，号召人们为某个事业献身是危险的，而政治和文化的进步不是一蹴而就的，秋瑾在我看来更是一个牺牲品，缺乏独立思考的幼稚的牺牲品。<br />
<br />
4/5/2009<br />
-----------------------------------<br />
<br />
拒绝造新烈女<br />
<br />
中国历来都有把女人树立成道德和勇气的榜样的传统。读读西汉刘向（公元前77－6）的《列女传》，里面的一百多个女性从不同角度代表了中国传统的理想人格：忘我、为公、为理想献身、为取得更高的道德实践律己、刚烈、宁折不弯，为大义而生死从容等等。特别是《节义传》里的十五个女性，个个刚烈不阿，为原则和正义，献出生命在所不惜。（想看这本书的（带图的）在这里：<a href="http://etext.virginia.edu/chinese/lienu/browse/scroll5.html" target="_blank">http://etext.virginia.edu/chinese/lienu/browse/scroll5.html</a>。我让我的学生读《列女传》，还给他们看图。让他们了解中国传统和中国人对女英雄的想象和塑造。我的学生对中国的烈女很是倾慕，问中国是否有《烈男传》，我想了想，还真不知道。<br />
<br />
我对我的学生说，《列女传》是教育女性的，写作者当然是男的。男人做不到的，中国女人都做得到。比如一个姑娘大街上看到一个男人小便，无意看到了那个男人的生殖器，这个女人就主动把眼睛弄瞎了，刚烈地成为中国的道德楷模。男人看女人的乳房和屁股，不存在这个问题。是不是？再说，中国男人很爱中国女人，希望她们都成为列女节妇。给女人树立的榜样层出不穷，男人们看到女人都这么好，经常说，老爷们都不如一个女流，他们也能上进，所以，女英雄的形象更感人，女烈女更壮烈，女烈士更激动人心，更让人们唏嘘不已的仰慕。在中国救国的尽是女英雄，比男的多多了，都很著名。<br />
<br />
为了跟所谓“黑暗的中国”斗争，呼唤人民起义，湖南人陈天华1905年沉海自杀。据说他写绝命书说“以死来激励国人“共讲爱国”。他主要的批判对象是清政府，他常常痛斥清朝政府是“洋人朝廷”。现在我们都知道他说得多么不合事实。1905年的清政府也没有那么僵化和落后。清政府1904刚刚开始停止科举制度，进行全面教育、政治改革。不知为什么陈天华的故事，知道的人不多，而秋瑾在两年之后以陈天华的精神进行暴动，进行暗杀等等，恨不得立刻把清朝推翻，成为二十世纪最大的爱国女英雄。也许根本的是，女的是英雄，比男的英雄动人。今年四月，也就是本月，一个新的秋瑾的电影就要公演了。电影是英文的，拍摄者是美国的华人电影艺术家。我一个月前收到他们的电影广告，打开电影广告，我看着挥剑的秋瑾，不知该说什么好。对秋瑾的崇拜，现在因为很多人有政治改革急迫感，就更如燃眉了。<br />
<br />
我认真读《列女传》还是来美国之后才开始的。美国对这本书的研究似乎比中国要深入。我先看的是学者的著作，然后去看《列女传》。让我吃惊的是，二十世纪中国推崇的女英烈在本质上和这些列女都差不多，她们共有的是这样的品质：舍己救国，用自己的鲜血唤醒或激发人民。因为这样的历史感，我再看中国今日人们推崇的英雄，忍不住比较起来，实在是不愿加入赞美烈女的大合唱，更拒绝写新的烈女或圣女传。实话说，我不是不相信或不尊重这些女性的英勇行为，而是对利用这些女性的公众话语非常警觉。我因此说，拒绝造英雄，也就是拒绝写新的烈女传。<br />
<br />
历史是一面镜子。没有历史感的人会觉得自己才开创历史。我对自动非要去崇拜英雄，如同人们信宗教一样，无话可说。可是我自己非常警惕，很怕那些刘向们，写了烈女让人们模仿，而烈女们，在两千年的中国社会里，好女人哪个不想成烈女？我小时候就渴望到珍宝岛跟苏联打一仗好当少年烈士呢。<br />
<br />
我也许真是虚无主义者。我不相信过于美丽圣洁的言辞，对造英雄的话语极度反感，可是没有模仿的榜样，我们能否说服自己？我们是谁？是什么？<br />
