													<h2>马化腾等建言互联网实行“特区制”</h2>
						
						
																											<div id="postmessage_536773" class="t_msgfont">3月28日凌晨消息，近期的互联网严打风暴造成十几万家网站被关，除了中小网站外，一些互联网大佬对此也有看法。在昨日的一个公开会议上，马化腾、王志东、丁健、王维嘉四位高管都抱怨互联网一刀切的监管方式应改进，王志东、丁健还建议深圳成为互联网监管特区。<br />
<br />
大佬们的集体抱怨<br />
<br />
2010年中国IT领袖峰会今日将在深圳开幕，作为本次峰会的一项活动，深圳市互联网产业发展咨询会昨晚在深圳举行。多位互联网及IT产业的高管参加了咨询会。这次咨询会最让人印象深刻的是，有四位IT行业的企业家抱怨互联网一刀切的监管方式。<br />
<br />
美通无线董事长兼CEO王维嘉最先提起这个话题。王维嘉不久前参加了《创业家》杂志社举办的一个论坛，主题便是国内互联网创业环境的讨论，其中包括几家本次互联网严打波及到的企业代表。<br />
<br />
王维嘉在咨询会上说，对于互联网公司而言，最重要的是公平竞争，十几万家网站被关闭，这里面可能会有阿里巴巴、腾讯、百度这样的巨头被扼杀了。<br />
<br />
紧接着王维嘉的发言，国内互联网巨头腾讯创始人、董事局主席兼CEO马化腾在发言中也提到了一点，他说，相关“因为管理的便利性”，在监管中对一些网站实行了一刀切的方式，特别是深圳又处在南方，与政策中心的北京较远。<br />
<br />
在马化腾之后，新浪网创始人、点击科技CEO王志东也声援前两位嘉宾。王志东也参加了上文提及的互联网创业环境研讨会，他对此也深有感触。王志东说，中国网民有近4个亿，这需要监管，而互联网又是一个需要革命性的、不断创新的行业，这两者之间存在一些矛盾。<br />
<br />
金沙江创业投资董事总经理、美国亚信公司董事长、联合网视文化发展有限公司董事长丁健在发言中也对这一话题进行了回应，他说，互联网要鼓励创新，要允许犯错误。<br />
<br />
王志东、丁健建议深圳成为互联网监管特区。<br />
<br />
上述四位企业家作为深圳市政府发展互联网产业的高级顾问，对于他们提出的互联网监管问题，他们都向深圳市政府提出了自己的建议。<br />
<br />
王维嘉建议深圳市政府给当地的互联网企业创造一个公平竞争的环境，不要让他们受到不必要的干扰。<br />
<br />
王志东认为，解决互联网监管与创新难题的最佳方法就是深圳。他说，“深圳一直有特区的文化和血液，一些互联网的创新与现有监管模式的矛盾在全国解决起来不容易，但深圳作为特区，是不是可以搞一个大的试点？”<br />
<br />
丁健的建议更是大胆，他建议深圳应尝试成为互联网监管特区，“互联网在深圳可以完全放开， 不要防火墙过滤，应该看看再更加自由的环境里，互联网是更乱了还是怎么样，这样才可能探索出一个适合中国的互联网监管方式。”<br />
