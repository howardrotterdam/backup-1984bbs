<br />
&nbsp; &nbsp; 小红花奖<br />
&nbsp; &nbsp; 提案水平<br />
&nbsp; &nbsp; <strong>超越小学生</strong><br />
&nbsp; &nbsp; <strong>政协委员巩汉林</strong>拍着胸脯称：“今年的提案水平很高，不会再是小学生水平。”以前巩汉林的提案是不是小学生水平我们暂且不论，至少今年已经超过了小学生水平，他用一年时间形成的提案确实是很多民众也关心的问题《保障听证制度顺利进行》。<br />
&nbsp; &nbsp; 所以我们隆重地把小红花奖授予他，希望全国政协委员明年给自己提出更高的目标，向高中生迈进。<br />
<br />
&nbsp; &nbsp; 劳苦功高奖<br />
&nbsp; &nbsp; <strong>秘书说的，都是我的思想</strong><br />
&nbsp; &nbsp; 据财新网报道，某善提议案者，每年都要提多份议案，总说自己忧国忧民，做好代表不容易，每年都要用两三个月时间到基层调研。但谈到具体问题，却多次顾左右而言他，甚至“忘记”了自己的观点，最后拉出自己的秘书。<br />
&nbsp; &nbsp; 他面带微笑地指着秘书对记者说：“有问题你可以找他，他说的都是我的思想。”<br />
&nbsp; &nbsp; 此情此景，情何以堪啊，看来做一个好代表不容易，做一个好秘书更不容易。故此授予这样的好代表以及他们的秘书“劳苦功高奖”，以兹鼓励。<br />
<br />
&nbsp; &nbsp; 最佳动作指导奖<br />
&nbsp; &nbsp; <strong>请给个信号，让我们鼓掌</strong><br />
&nbsp; &nbsp; 据财新网报道，一位来自香港的委员说：“我要提一个建议。每次列席人代会的时候，听政府工作报告，起码有上十次的掌声。其实政协的报告讲得也很好，我也想鼓掌，但看到大家都没有鼓掌，我也就不好意思鼓掌。我们能不能有个引导性的信号，鼓鼓掌表示一下赞同。我很想说这个。”<br />
&nbsp; &nbsp; 以前经常听到明星们在冷场的时候假装亢奋地高呼“掌声在哪里！”“让我看到你们的双手！”但是在两会这么庄严肃穆的时候显然不能这么做，于是这个“引导性的信号”似乎有点道理。<br />
&nbsp; &nbsp; 凭借此提议，这位来自香港的委员与袁和平、程小东等来到同一高度，荣膺最佳动作指导奖。<br />
<br />
&nbsp; &nbsp; 最爱学习奖<br />
&nbsp; &nbsp;<strong>我们来开会，就是听领导说</strong><br />
&nbsp; &nbsp; 一些两会代表委员很爱学习，他们说自己去北京就是要去学习。财新网记者去采访来自基层的代表，认为他们熟悉民情，对问题感受深刻，结果人家说：“我们是来学习的。领导讲话，我们在会场上认真听领导说，回去做好总结传达，把事情做好就够了。”<br />
<br />
&nbsp; &nbsp; 已故中共元老王震之子、曾任中信集团董事长多年的<strong>王军</strong>委员在被凤凰网记者问到所关注的问题时，他也是谦逊地说“我啊，我是来听一听，来学习的。”<br />
<br />
&nbsp; &nbsp; 深明大义奖<br />
&nbsp; &nbsp; <strong>倪萍：我爱国，从不反对或弃权</strong><br />
&nbsp; &nbsp; “在大的会议上举手表决时我从来没有反对过或弃权过。”全国政协委员、著名主持人倪萍说。在接受中广和网易采访时，被问到是否行使过否定的权利，倪萍说，我是(考虑)国家利益的，我热爱这个国家，我没有打断过，也没有弃权过。但对于不同的意见，我会在文案中写上再递上去。”<br />
&nbsp; &nbsp; “就党和国家的利益，在目前这个阶段，要是你想不出比它更高的招，你就应该拥护。”<br />
&nbsp; &nbsp; “我不添乱，不能说你把人骂倒了再一问你怎么办你不知道。谁都知道有问题，你以为中央领导不知道啊？千万别以为你的智商比他们高，没有。腐败干部有没有？能力差的有没有？有，但是这样的，是大多数吗？是非常少的少数。”<br />
<br />
&nbsp; &nbsp; 是否反对过或弃权过的问题也抛给了全国政协委员葛剑雄、濮存昕。<br />
<br />
&nbsp; &nbsp; <strong>葛剑雄</strong>说自己投过弃权票，“在我没有把握，又得不到有力解释的情况下，会投弃权票。”<br />
