													<h2>政协委员倪萍“不反对”引争议</h2>
						
						
																											<div id="postmessage_510311" class="t_msgfont">华西都市报<br />
<br />
　　一句“我爱国，我不添乱，从不反对或弃权”，使倪萍参政议政能力被质疑<br />
<br />
　　这几天，全国政协委员、著名主持人倪萍，成了众多读者关注的热点人物。一是在五天前政协文艺界分组讨论会上，一组倪萍委员跪地为冯小刚等其他委员拍照的照片煞是夺人眼球。报道说倪萍转行做了“娱记”，为达目的不择“姿势”的专业精神，值得认真学习。虽是花絮，然调侃声中，亦颇有耐人寻味之处。二是，在两会上，倪萍一句“在大的会议上举手表决时，我从来没有反对过或弃权过”，立即引发了媒体和观众的关注和质疑。昨天，凤凰电视台著名主持人吕宁思，一针见血“炮轰”倪萍的两会“雷人”语言。他直言道：倪萍这样的代表委员肯定不合格。<br />
<br />
　　倪萍的这番“实话实说”也在媒体上引发争议。《半岛晨报》评论以赵本山、宋丹丹的小品来调侃倪萍：“那是相当地……”当白云(宋丹丹)手舞足蹈地向小崔(崔永元)描绘签售的火爆场面时，实在听不下去的黑土(赵本山)感觉“胃疼”。作为多少有点现代政治常识的中国公民，倪萍上面的话，也让人感觉有点“胃疼”。倪大姐说这番话时，内心一定是相当真诚的，和虚荣且爱虚构的白云应该完全不一样。但倪大姐的真诚正是人感觉“胃疼”的原因之一：一个人如果揣着明白装糊涂，至少还是可以心照不宣地交流的，如果真是糊涂，那就难了，<br />
<br />
　　如果恰巧对方是一个人生观和世界观都已定型的成年人，那就更难了。对倪萍客串娱记拍冯小刚，《南方日报》发表署名文章，评论：倪萍委员委实无需客串“娱记”。文中称，根据“两会”新闻中心的信息，今年报名采访“两会”的记者共达3060名，就差了“古”，“今中外”一应俱全，其中可能不乏新丁，但均为专业人士，她完全不用为报道的事情担心。身为政协委员，如果说体现专业精神的话，更应该在履行“政治协商、民主监督”的人民政协主要职能上去体现，要看她参政议政了些什么。 <br />
 <br />
<br />
　　《京华时报》评论：倪萍这样的态度与“我是来向各位领导学习的”、“这个话题很敏感，我不好说”、“不成熟的看法请大家指教”、“我讲出来供您参考”等等，基本相仿。是一些代表委员以从属与附和的姿态而非民意代言人的身份，在两会会场上行使仅仅属于个人态度的权力。（记者杜恩湖）</div>

							
							
							
							
