													<h2>倪萍：谁说赞同的声音没有思想</h2>
						
						
																											<div id="postmessage_512134" class="t_msgfont"><img src="http://www.chinanews.com.cn/yl/yl-mxzz/news/2010/03-11/U253P4T8D2162810F107DT20100311081205.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
<br />
 <br />
<br />
　　昨天下午，文艺界别分组讨论会，处于舆论中心的倪萍第一个发言。在讲道德建设时，倪萍为自己喊冤，并针对批评声音说：“谁告诉你反对的声音才有思想啊？赞同的声音就没有思想吗？”<br />
<br />
　　对于网上反复炒她说的那句话，倪萍有点气愤，她说媒体没有过问她为什么赞同。<br />
<br />
　　趁着倪萍接电话，记者在会场外对她做了简短的采访。提到网上的质疑，倪萍说，她对这个事非常坦然，没觉得有什么压力。“我觉得这种声音是很弱小的，很幼稚。他告诉你反对的声音才是有思想的。谁说的？反对的声音也有思想，但是不是绝对的。赞同的声音就没有思想吗？”<br />
<br />
　　倪萍认为，媒体对自己所说的话断章取义，认为她是名人，故意先“砸一棒子”。“当然国家发展中肯定有问题，但是你出来反对就解决得了了？你老是骂，骂骂这社会就好了？” <br />
<br />
爱国就是不投反对票？倪萍回答：“这是两回事。爱不爱国与投反对票和赞成票没有关系。我相信投反对票的人也爱国，他也有他的道理，只是我不知道他的道理。”<br />
<br />
　　倪萍认为，自己投赞成票是从大局利益出发，自己的选择是经过深思熟虑的。当被问及在投票时的选择能不能代表人民利益时，倪萍说：“太能了。我自己就是人民，就是一个普通的老百姓。”<br />
<br />
　　在被问及为什么不投反对票时，倪萍用一个家庭来比喻国事。“一个家庭，孩子特别理解父母当家的难处，应该跟着父母一块走，一块克服困难，一块去解决问题。”<br />
<br />
<strong>“大局利益”应该是老百姓的大局利益</strong><br />
<br />
　　“投不投反对票，最重要的，是政协委员的选择，应该是一种独立的民意表达。”―――　　中国社会科学院政治学所研究员刘山鹰<br />
<br />
　　“投不投反对票，最重要的，是政协委员的选择，应该是一种独立的民意表达。中国不需要那些谁都不愿意得罪的委员或代表。”中国社会科学院政治学所研究员刘山鹰认为，如果“不投反对票是为了爱国”是这位委员的真实想法，那么只能证明她不具备参政议政的能力，更不具备政协委员为民请命的专业素质，建议不要当政协委员。<br />
<br />
　　提到不投反对票是为了爱国，是从“大局利益”出发的言论，刘山鹰分析说，爱国的本质实际上是爱民，为老百姓请命，因为一个国家实际上就是由千千万万的普通百姓构成的，爱民才是真正的爱国。至于“大局利益”，他说：“这同样是经不起推敲的。大局利益，指的是谁的利益？我们该用手中的表决器彰显的，是大多数老百姓的大局利益。而不是少数既得利益者或官员的利益。”刘山鹰认为，温家宝总理在政府工作报告中提出要让老百姓活得有尊严，其中一项重要内容，就是老百姓的意愿能得到反映，这是政协委员应尽的义务。<br />
<br />
　　如果把国家比喻成一个大家庭，刘山鹰认为，不能简单地因为当家人有难处，就不支持大多数成员的呼声。<br />
<br />
　　谈到有委员建议“给表决器加盖”，刘山鹰说：“我认为加一个盖子还不够，最好是能设置一个秘密投票处，来保证谁投了什么票不被泄露。”他认为，每个人都会顾及自己的选择被人知道后，会不会对自己造成不利影响。“尤其是在人事任免上，这种担心可能更多。当然，这种担心是无可指责的。但我们可以增强个人投票的保密性。”刘山鹰认为，应该设置秘密投票处，让委员、代表在纸质的选票上画钩。“这就能够保证每一个人的投票情况不被查出。”（谢佳君）</div>

							
							
							
							
