													<h2>倪萍：不是投反对票就一定更有思想</h2>
						
						
																											<div id="postmessage_512567" class="t_msgfont">倪萍回应不投反对票：不是反对就一定更有思想<br />
<br />
2010年03月11日08:07<br />
<br />
<img src="http://gb.cri.cn/mmsource/images/2010/03/05/22/2907453085733308530.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　全国政协委员、知名主持人倪萍因“我爱国，不添乱，从没投过反对票或弃权票”的言论引发网络争议。倪萍10日在小组会结束后向媒体回应说，自己没有因舆论负评而有压力，她之前的话是实话实说。<br />
<br />
　　“我说我不给国家添乱，是出于对国家和民族的热爱，结果网上炒成了，‘不投反对票是没脑子、没本事’，‘要这样的政协委员干吗？’”倪萍说，“99%的委员都支持(投赞成票)，你能说都错了吗？”<br />
<br />
　　她说舆论还有一种误解，就是认为反对的人一定都是有思想的人。“我认为反对的人的观点也没什么错，应该是经过思考的，是有道理的，只是我不知道他的道理是什么。但并不是反对者就一定比支持者更有思想。”倪萍打了一个比喻说，“一百个人往东跑，另一个人往西跑，大家会说往西跑的人有个性，但仅仅是有个性而已，他不一定是胜利者。”<br />
<br />
　　倪萍又补充说，另一方面，也不能认为多数人就一定是正确的，“这还要历史地看，今天我们谁也不下这个结论”。<br />
<br />
　　被媒体问及目前的负面评价会不会造成压力，倪萍说：“不会有压力，也不会就此按照别人说的那样去投反对票。”她又说，“我是有错勇于承认，但没错想让我承认是不可能的”。<br />
<br />
　　她认为不投反对票还是出于爱国及对国家的理解，“这就像孩子能理解父母在抚养过程中出现的问题，有时候顾得上老大顾不上老二”。<br />
<br />
　　倪萍也承认，政府不都是对的，“政府有的问题是公开解决，有的是内部解决”。她认为，批评政府可以，但关键要看出发点，“如果骂只是为了痛快，那是噪音。如果为了人民，实事求是，才是有力量的批评”。<br />
<br />
