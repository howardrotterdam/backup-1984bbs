													<h2>香港拒绝被高铁 广深港高铁香港段造价太高遭反对</h2>
						
						
																											<div id="postmessage_418214" class="t_msgfont">香港拒绝被高铁 广深港高铁香港段造价太高遭反对 <br />
 来源：时代周报 作者：戴平 2010年01月07日<br />
　在大陆高铁网络正有条不紊建设的同时，作为中国“四纵四M”客运专线京港客运专线的组成部分，广深港高铁香港段的建设计划却迟迟不能付诸实施。就在香港特别行政区政府争取立法会通过高铁拨款前夕，反对高铁的声音越来越大，建还是不建，一场拉锯战在政府与民众之间展开……<br />
<br />
　　这边厢，大陆的武广高铁已于去年年底正式通车；另一边厢，连接香港与大陆的广深港高速铁路香港段（下称香港高铁）拨款，需延迟至本月8日才由香港立法会审议。<br />
<br />
　　相比大陆高铁建设的顺利，香港特别行政区政府争取立法会通过高铁拨款前夕，社会上却掀起一股股争论的浪潮，从项目斥资600多亿港元是否物有所值，到香港区总站应设在市中心还是市郊，及至项目会否破坏生态环境……凡此种种，政府与民间展开了一场拉锯战。<br />
<br />
　　十年谋划项目遇阻<br />
<br />
　　兴建高铁这个构思可以追溯到1997年亚洲金融风暴爆发后。当年香港经济受到重创，时任香港行政长官董建华眼见经济前景暗淡，认为香港若再不和大陆加强联系，只会走入死局，于是主动同广东商讨合作。<br />
<br />
　　然而由于种种原因，香港高铁的兴建计划一再延迟。<br />
<br />
　　2005年9月，国家发展和改革委员会正式批复了《广深港铁路客运专线广州至深圳段可行性研究报告》，同年12月18日，广深港高铁正式动工兴建。预计今年7月，广州至深圳北站将通车运营。<br />
<br />
　　相对广深段的建设速度，香港高铁行动缓慢，香港运输及房屋局局长郑汝桦去年11月在立法会铁路事宜小组上，罕有地以强硬姿态推介广深港高铁，并认为目前广深港高铁项目已进入关键时期，若香港不建，便会沦为孤岛。<br />
<br />
　　根据香港政府的设想，广深港高铁香港段将会由香港的西九龙至港深边境的落马洲一带，线路全长26公里。开通后，将会连接国家正在兴建、长达16000公里“四纵四横”的高铁网络，让港人迅速到达其他大陆城市：5小时到武汉和厦门，8小时到上海，到北京也只需10小时。届时高铁将为香港带来庞大的经济效益，每年预计可为港人节省4000小时交通时间，50年的经济效益达850亿港元。这还未计算旅游和投资的收益。<br />
<br />
　　香港中文大学亚太研究所前所长杨汝万接受本报采访时警告，香港是整个大陆最边缘的地方，若不能与大陆在交通上接驳，肯定会被边缘化。他指出，在5-10年内，中国高速铁路网将会大规模覆盖，“香港当然可以选择什么都不做，但不做只会被边缘化，人家跑你不跑，商家便会去深圳做生意而不去香港。”<br />
<br />
　　香港城市大学专上学院社会科学学部去年5月在全港多区进行街头访问，1000多位港人受访者普遍表示支持广深港高铁项目。84%的受访者表示“非常赞成”或“大致赞成”，表示“不太赞成”“极度反对”的仅占16%。<br />
<br />
　　但在官方及社会主流意见基本认同应该兴建高铁的时候，从去年下半年开始，香港社会开始出现反对的声音。<br />
<br />
　　造价太高遭反对<br />
<br />
　　高春香是反对者中的一位关键人物。这位47岁的女性，是受高铁项目影响需要迁拆的菜园村的原居民。<br />
<br />
　　曾经，高春香还在做社工时，便捷的交通让她受益匪浅。然而后来，高春香开始反省，快捷固然重要，但追求快捷必会有人付出代价，一切当珍惜眼前。高春香接受本报采访时说，在她向往的多元生活方式中，乡郊的闲适不能失去，那是她生命的根源。因此，她到社会工作后，曾一度在外面居住，但不久又搬回菜园村。<br />
<br />
　　当港府提出兴建高铁，要求菜园村迁拆的时候，高春香二话不说，立即发动居民成立菜园村关注组，并出任主席，强烈坚持要求保留菜园村，不拆不迁。<br />
<br />
　　接受采访时，高春香正忙着印制文件，准备与政府周旋。她叹息道：“我们的坚持是有理由的。除了因为居住多年，悠闲耕作不也是许多人憧憬的退休生活吗？只是许多人不知他们现在已经可以拥有罢了。”<br />
<br />
　　菜园村关注组的出现，开始唤起港人对高铁项目的关注，多个反对兴建高铁的不同利益团体也先后诞生。<br />
<br />
　　综观目前香港高铁的反对者，大致有两批人：一批是从支持菜园村农民拒迁开始，逐步转变为反高铁的上世纪80年代出生的年轻人，他们主要是对高铁项目破坏居民的生活状态、引起空气和噪音污染表示愤慨。另一批人则是政党及“公共专业联盟”，他们批评高铁，有太多问号当局仍未交代，现阶段无法让高铁项目合理化。他们质疑：当局提出每日99000人次乘客量的假设依据是什么？总共600多亿港元的造价，也让人难以接受。<br />
<br />
　　症结不仅仅是菜园村<br />
<br />
　　一开始，香港政府对于这少数人的声音并没有给予应有的重视。为使发展高铁合理合利，港府信息发布的重点主要集中在三方面：一、倘香港建不成高铁，势必不能与大陆接轨，最终会沦为竞争力落后的孤岛；若延误兴建，也会丧失先机。二、强调高铁的好处，包括节省时间、合理票价、减少排碳、促进就业及贯通大陆。三、把菜园村征地事件简化成赔偿问题，征地被反对，是赔偿不足，“加码”自可息事宁人。<br />
<br />
　　2009年11月21日，香港政府宣布提出特惠补偿安排，除提高菜园村征地赔偿，赔偿额大增140%外，为帮助受影响的村民安排日后的生活，政府还允诺，真正务农的村民可以另觅农地务农，也可以选购附近居屋或者村屋等。<br />
<br />
　　政府的举措在短期内收到了成效。然而随着香港媒体对高铁项目的信息发布越来越多，民众逐渐发现，当局并没有在一些问题上响应社会的诉求。其中，造价方面引起的不满最为严重。<br />
<br />
　　“反高铁・停拨款大联盟”指出，港府最终的方案是要向立法会申请652亿港元，连同16亿港元工程备用开支，立法会要批复的拨款高达668亿港元，加上征地和赔偿菜园村居民的开支约20亿港元，整项工程所需接近700亿港元，即每位香港市民平均需要承担约1万港元。这比政府在2008年初公布395亿港元造价增加了差不多一倍！<br />
<br />
　　“造价忽高忽低，不仅未能起到让市民逐步接受较高造价的效果，反而制造了混淆，使人质疑政府的方案是否有不可告人之处。”“反高铁・停拨款大联盟”一名成员说。<br />
<br />
　　于是，“反高铁・停拨款大联盟”和一群80后反高铁青年在去年年底开始游行，动员群众向立法会施压，要求停止高铁拨款。而“公共专业联盟”则召开记者会，要求政府再详细考虑高铁的利弊。<br />
<br />
　　2009年12月18日，香港社会反高铁抗争掀起高潮。当日，立法会财务委员会审批广深港高速铁路计划拨款。会场外，近2000名市民高叫“没有抗争，哪有改变”；会场内，泛民议员以车轮战术质询政府，最终成功让高铁拨款被迫押后至本月8日表决。场外市民闻讯后纷纷相拥狂呼，在立法会门外绕场一周以示胜利。<br />
<br />
　　“政府处理手法仍有不足”<br />
<br />
　　目前，临近8日香港立法会财委会高铁拨款审议，政府和反高铁团体的角力也进入白热化阶段，纷纷尽最后努力“拉票”。<br />
<br />
　　据了解，“反高铁・停拨款大联盟”的数名80后反高铁青年，近日还将发起“五区苦行”行动，呼吁市民一同反对拨款。大联盟还会在8日到立法会财委会门外“包围”立法会。<br />
<br />
　　而港府一方面不断向公众强调，香港段的工程每延误一天，估计会损失500万港元因节省交通时间带来的经济效益，并发动支持高铁的专业人士出面表态支持港府。另一方面，政府近日逐一致电支持高铁的议员，呼吁他们在8日财委会会议上留守至投票一刻。<br />
<br />
　　据本报记者了解，多名支持高铁拨款的立法会议员，近日已收到政府官员来电“数票”，询问他们在8日财会上是否会留守至投票。其中，独立议员叶刘淑仪为投支持票，已同意缩短日本旅行行程回港。<br />
<br />
　　至于获得不少同情的菜园村，在政府制定了各种补偿方案后，已有逾八成村民向当局登记接受补偿。据官员估计，一旦财委会通过拨款，接受政府赔偿的村民将会更多。<br />
<br />
　　香港政坛人士分析，现在的立法会财委会中，支持高铁的议员占多数，估计高铁拨款将获得通过。一旦通过，意味着香港高铁最快有望于2015年落成。<br />
<br />
　　结果即将确定，但对于香港政府在高铁项目上几遭滑铁卢，立法会议员梁美芬接受本报采访时批评，凡有新政策和措施推出，除了事前要做到充分咨询外，执行期间顾及利益相关者的感受也相当重要，但当局似乎在推动高铁的期间，没有设身处地去考虑这些人的忧虑和关注所在。<br />
<br />
　　她举例说，政府要在大角嘴地底挖掘高铁隧道，一些旧楼的业主，特别是一些老人家，由于对工程缺乏认识和了解，显得十分忧虑。她早前跟“高铁大角嘴工程监察小组”在该区域内作过一项问卷调查，发现95%以上的居民对兴建高铁表示担心。主要原因就是政府事前咨询不足，事后解释又不清楚。<br />
<br />
　　“其实，举凡这种工程浩大的项目，政府都应该有政治考虑，不能简单相信单从经济和专业角度，就能消除所有的恐慌。兴建高铁本身是有利全香港发展的事，但从政治和公关角度来看，政府处理手法仍有不足，以致反对声音四起。”梁美芬认为。</div>

							
							
							
							
