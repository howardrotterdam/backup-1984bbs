													<h2>《人民日报》刊发中国公民致哥本哈根的信</h2>
						
						
																											<div id="postmessage_367450" class="t_msgfont">一个中国公民致哥本哈根的信<br />
 2009-12-11<br />
<br />
　　12月7日，在丹麦首都哥本哈根的联合国气候变化大会举办地贝拉中心，两名与会人员从一个模拟太阳的LED发光板前经过。<br />
<br />
　　星岛环球网消息：《人民日报》12月11日刊发题目为《一个中国公民致哥本哈根的信》的信，文章内容如下： <br />
<br />
　　哥本哈根气候大会的各国代表： <br />
<br />
　　你们好。这些天来，你们在开着世界上最重要也最难熬的会。你们的争吵，我们都听到了。 <br />
<br />
　　我是一个出身农民家庭的普通中国人，刚刚过上比上辈人更好些的日子。拥有更美好的生活，是每个人的愿望。在我的老家，农民们买电器、盖房子，希望到大城市去转转。在我居住的城市，空调越来越多，汽车越来越多，很多人都在为一个稍稍宽敞些的家而奋斗。 <br />
<br />
　　然而我们不幸生活在一个尴尬的时代。很多人刚想把他们羡慕的欧美式生活从银幕上搬进生活，就被断然喝止。我在农村的父母还没来得及在越来越热的夏天买一台冰箱、装一台空调，听说这些产品就要被征收碳排放税。我同学上班的工厂前些天刚刚倒闭，因为它更换不起符合环保标准的设备。那些生活在岛国的外国朋友就更可怜，据说地球只要再升温1.5度，他们的国家就没了。 <br />
<br />
　　我和我周围的人，当然希望有一个更清洁、更凉爽、更少气候灾难的环境，并为此付出我们能够承受的代价。但我困惑的是，这个代价究竟该怎样由地球上的60亿人分担？发达国家、发展中国家差别巨大的生活方式，是不是应该永远穷国的归穷国，富国的归富国，像世袭制一样代代相传？假如是这样，我们为未来奋斗的动力在哪里？ <br />
<br />
　　我去过中国西南、西北的一些贫困地区。那里的一些人生活还相当艰难，有的人还在为温饱奋斗。但他们生活的环境，已经受到气候变暖的影响：天气更恶劣，风沙更凶猛，灾害更频繁。他们吞食着世界快速发展留下的苦果，而种下这苦果的人和他们的后辈，很多却在万里之外过着幸福的生活。他们对后发展起来的人说“到此为止”，仅仅是因为这个世界装不下这么多幸福。 <br />
<br />
　　“到此为止”――作为一个进城不久的乡下人，摸着自己的良心，我说不出这样的话。我没有权利要求我的乡亲用自己的落后去支撑他人的幸福，正像我没有权利要求所有富人扔掉别墅、停开房车一样。我能做的，只是人走灯灭、使用节能电器、洗澡时少用点水、穿简朴的衣服……过简单而不失快乐的生活。 <br />
<br />
　　好在我的周围，越来越多的人已经加入这一行列。他们不仅自己追求健康、绿色的生活方式，还自发去贫困地区推广沼气和太阳能，去干旱地区帮助改变传统种植结构，去泥石流滑坡地区促改梯田，帮助当地百姓应对气候变化。更有一些人参加环保民间组织，监督企业排放，优化城市规划，维护民众环境权益。低碳、减碳、碳足迹这些新名词，在中国人眼里变得越来越熟悉。 <br />
<br />
　　“我们并非是从祖先那里继承了地球，而是借用了子孙的地球。”我们每个人用自己的行动对世界负责，对子孙负责。我们付出的每一分努力，都将在不远的将来获得回报，即使在我们离开这个世界之后，也会留下深深的足迹。 <br />
<br />
　　哥本哈根的会议，本意是为了人类的未来，而不是为了争权夺利。发达国家拿出诚意，在资金、技术等方面帮助发展中国家节能减排；发展中国家量力而行、尽力而为，不再走发达国家的老路――这是我，一个普通中国公民，对共同应对全球变暖的理解，也是我对这次会议的期待。</div>

							
							
							
							
