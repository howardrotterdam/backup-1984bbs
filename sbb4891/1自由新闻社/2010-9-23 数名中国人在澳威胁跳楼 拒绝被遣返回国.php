福建人偷渡的动力和来美后的生存手段<br />
<br />
福建人偷渡的动力： <br />
<br />
1。 先辈偷渡成功，源源不断地给家乡寄钱回来。年轻人，如果谁不偷渡就是没有出息。 <br />
<br />
2。 偷渡是要吃苦， 不过也就三年， 把债还了以后就是自由身。 不偷渡也吃苦，而且是一辈子吃苦， 每月拿$600-$800工资(福建地区)算是不错的了. <br />
<br />
我曾经与一位过去曾经偷渡、现在已经在美国开有餐馆的老板交谈。他说蛇头好， 蛇头给他们机会。 我说谁要是出国以后赖帐怎么办？ 他回答说，不会的， 人家给你提供机会， 哪有理由赖帐？ 而且又没有多少钱， 年纪轻打上几年工都能还得出。 <br />
<br />
我想正因为蛇头和偷渡客能长期维持这样有信用的稳定关系， 才能使得偷渡业务长期继续下去。 要是换了别的什么大城市(这里不便点名)的人， 各自打小算盘， 出国后人溜得无影无踪， 蛇头拿不到钱， 日后的生意也不能做下去。 <br />
<br />
据说偷渡一批就有十条船， 每条上面有100人， 这样就是1000人， 要按每人收费$40K 计算， 一次的生意就是$4000万。 哪里去搞那么好的生意啊？ 有了这笔钱，蛇头可以出$50万贿赂美国的边境守卫(他们的年薪才$2万多一些)。而且搞偷渡比贩毒罪名上要轻得多， 钱却不少赚。 <br />
<br />
不时听到偷渡有死亡的， 但是我估计大概死亡率不会超过1%， 因此可能性还是很小的。 <br />
<br />
他们来美国以后，语言不通、不能合法打工，怎么生存、和还钱呢？ <br />
<br />
蛇头有办法。 不是收了每人好几万元偷渡费吗？对于蛇头来讲，那是黑钱。 于是蛇头把那批新到移民组织起来， 比方以每十人为单位。 蛇头再把收到的钱回“借”一、二十万给那十个人合资， 让当地有身份的人挂名开个New China Buffet， 稍微经过一、二周的训练， 让其中有能力的人当经理、大厨， 就能开张。 <br />
<br />
去过New China Buffet连锁店的都知道， 那里真是价廉物美， 其它中国饭店根本没有办法与它竞争。 为什么呢？其中一个道理就是它没有劳动成本， 或者说在竞争中不受最低工资约束。 过去曾经有报到，说在纽约Chinatown一basement内查出32个福建非法移民， 而那里只有十六个床位。维持这样的生活水准是不需要最低工资的。 所以也不是所有人都能吃得起这个苦的。 <br />
<br />
[偏题一下：我曾经去过日本，那里的福建人也是坚苦耐劳。 他们可以找上两、三份餐馆工， 打完第一份工后乘地铁去第二份工作地点上班， 中间在地铁上可以休息一下， 打完第二份工后再乘地铁去第三份工作地点上班， 中间在地铁上可以休息一下，打完第三份工后再去第一份工的地方， ...。 中间只要能维持5-6个小时的睡眠就可以了。 所以那里的人说， 福建人可以一天24小时整天工作。] <br />
<br />
经过二、三年惨淡经营， 那些人把蛇头的“投资”还掉， 他们就自由了。 他们那个New China Buffe如果生意不好， 也不在有必要继续开下去， 关门就可以了。而蛇头这次收到的钱就是投资收回的“白钱”了。 <br />
<br />
是不是两全其美？ <br />
<br />
总之， 偷渡有动机、有甜头、有范例、有组织安排</div>

							
							
							
							
