													<h2>数名中国人在澳威胁跳楼 拒绝被遣返回国</h2>
						
						
																											<div id="postmessage_841117" class="t_msgfont">正值9名中国籍寻求庇护人士在维拉活非法移民羁留中心的屋顶上抗议之际，记者今日成功与之电话联系，了解这9人的诉求和现状。9人中唯一的孕妇小云告诉记者，“我们不得已以死抗争，对抗澳洲所谓的人权政府。”她称，若要求被拒，最迟今日下午将集体跳楼自杀。 <br />
<br />
　　小云在电话里称，羁留中心周一跳楼丧生的男子是他们的好友，平日里常在一起打篮球和聊天。后者跳楼时，小云等人均目睹现场，“头都摔裂了，太恐怖了”。据她称，自周一开始，包括小云在内，中心全体难民开始绝食，至今日已有3天。小云告诉记者，今日早上7点刚过，9人爬上2区大楼屋顶抗议，包括5男 4女，全部来自中国福建。“有留学签证过来的，有搭机入境的”，年龄分别在20岁到37岁不等。 <br />
小云称，他们现在情况“很不好”。“大家精神压力很大，我的头和胃都很痛”。她称，今日中午又一名21岁男子在屋顶上用瓦片割脉自杀，“流了很多血，嘴唇都白了，几乎昏倒滚下楼去”。不过，9人拒绝了当局接这名男子下楼就医的要求，“我们自己用衣服撕条帮他包扎了伤口”。小云说，“政府不答应我们的要求，我们是不会下楼的，我们大家共进退”。她称，中午还有两人差点跳下去。 <br />
<br />
　　据小云抱怨，迟至今日电话采访之际，没有任何一名移民官员到场与之商谈，“难民组织也不让进来，两个红十字会的人也被挡在门外，媒体全部都在外面马路边，不许入内”。她指责当局采用“拖字诀”。“保安不给我们水喝，要我们下楼再喝。我们已经绝食3天了，现在没有食物，没有水，也没有御寒的衣服”。她称，“有一个学生扔了瓶水上来，我们9个人每人只喝了一口”。 <br />
<br />
　　小云与其它一众9人的诉求，是要求当局“给我们自由，给我们永久性保护签证，不要强行将我们遣返回国”。她指责澳洲政府“所谓的人道主义国家，不管我们死活，连孕妇都关在里面”。她称自己在这处羁留中心已经被关押了76天。小云说，“我们又不是杀人犯，我们只是难民而已”。9人要求有移民局官员前往会谈，给出积极正面结果，并要有难民组织成员同行见证，“否则他们把我们骗下楼又变卦，还是会被强行送回国”。 <br />
<br />
　　被问及是否担心长时间绝食影响腹中胎儿健康时，小云表示“我死都不怕了，也顾不上了”。她在电话里告诉记者，“最迟明天这个时候（即周四下午约4点），如果他们还不答应我们的要求，我们9人会集体跳下去”。 <br />
<br />
　　就在采访即将结束之际，电话那边传来人群高喊口号声，“我们要自由（We Want Freedom）”。小云告诉记者，“又有红十字会的人被拒绝进来”。 <br />
<br />
　　澳大利亚福清同乡会理事长陈励强表示，该会已关注此事，但尚不知具体内情。他称，“这种事情发生后，尤其有孕妇在内的情况下，当局应给予重视，派出有社工经验和心理辅导经验的相关人士而非保安，积极主动与抗议者沟通并开解，以免当事人情绪失控，做出一些冲动之举”。他称会继续关注事态进展。<br />
