　　教材出错不是小问题，其负面影响不言而喻。教材的出笼有一套严格的程序，在教师和学生看来，教材是教育和学习的蓝本，其正确性几乎不容置疑。一旦教材出错，一是贻害教师、学生、家长甚至社会；二是影响教材的权威性；三是影响教材编写等相关部门工作的可信度和严肃性。而且，教材出错是温总理听课发现的，说明长期以来，对教材的使用效果，相关部门缺乏评价和反馈机制。<br />
<br />
　　把陕西、甘肃划入华北地区的初中地理教材由中国地图出版社出版。在老的地理教材区域划分中，华北地区只包括北京、天津、河北、山西和内蒙古自治区；陕西和甘肃划入西北地区。据介绍，地理教材至少三个版本，对“华北区”描述不统一。现在中学教材版本很多，比如地理教材至少有三种版本，总理听课的西城区用的是中图出版社的版本，海淀区的中学使用的是人民教育出版社编写的教材，还有的区县使用的是北京师范大学出版社编写的。”北京市中学地理特级教师白洁说。据介绍，在海淀人教版地理教材中华北地区与老教材一致，仍为北京、天津、河北、山西、内蒙古自治区。<br />
<br />
　　把陕西、甘肃划入华北地区不对，尤其是将甘肃划入华北很荒唐，因为从地域、地理、经济等方面来看，甘肃都跟华北比较远，应该划入西北或者西部。<br />
<br />
　　如此看来，对于教材，相关部门应该组织专家来一次“大体检”，以免还存在错误流毒社会。<br />
