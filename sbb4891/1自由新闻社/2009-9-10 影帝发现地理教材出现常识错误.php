																											<div id="postmessage_234682" class="t_msgfont">Wen Jiabao听课时发现地理教材将陕甘划入华北<br />
2009-09-10 08:27:34　来源: 新华网<br />
核心提示：9月4日，Wen Jiabao总理在北京35中学听课时指出了地理教材中的错误。该校选用的地理教科书把陕西、甘肃划入华北地区。据了解，目前中学地理教材至少三个版本，对“华北区”描述不统一。<br />
<br />
在北京市第三十五中学，Wen Jiabao在旁听初二（五）班的课。李学仁 摄<br />
在北京市第三十五中学，课间休息时，Wen Jiabao和同学们讨论上课的内容。<br />
新华网9月10日报道 9月4日，Wen Jiabao总理到北京35中学听课。据新华社等媒体报道，Wen Jiabao总理赞成地理课把地理和地质、气候结合起来，也指出了教材中的问题――教材对我国地区的划分不清楚，甚至有错误，缺乏自然的或行政的依据。昨天记者通过采访了解到，该校选用的地理教科书把陕西、甘肃划入华北地区。<br />
<br />
陕甘划入华北地区<br />
<br />
总理听课发现问题<br />
<br />
当天上午，北京35中学地理老师纽方萍上的地理课，内容是第九章“中国区域的差异”。华北地区是本章节的起始课，也是这节课的上课内容。课堂上纽方萍老师通过提问学生去了哪些地方让学生认识区域的差异，同时还通过投影显示了华北地区包括的省市自治区。<br />
<br />
“Wen Jiabao总理非常赞赏学生去了很多地方，比如南京、台湾、欧洲等，也赞成地理课把地理和地质、气候结合起来，同时指出了教材中的问题。”纽方萍老师回忆说，“温总理认为新版本的教材区域划分有问题，陕西、甘肃不应该划在华北地区中。”<br />
<br />
把陕西、甘肃划入华北地区的初中地理教材由中国地图出版社出版。在老的地理教材区域划分中，华北地区只包括北京、天津、河北、山西和内蒙古自治区;陕西和甘肃划入西北地区。<br />
<br />
地理教材至少三个版本<br />
<br />
对“华北区”描述不统一<br />
<br />
“现在中学教材版本很多，比如地理教材至少有三种版本，总理听课的西城区用的是中图出版社的版本，海淀区的中学使用的是人民教育出版社编写的教材，还有的区县使用的是北京师范大学出版社编写的。”北京市中学地理特级教师白洁说。据介绍，在海淀人教版地理教材中华北地区与老教材一致，仍为北京、天津、河北、山西、内蒙古自治区。<br />
<br />
北京市中学地理特级教师张凯接受记者采访时明确表示，把陕西、甘肃划入华北地区不对，尤其是将甘肃划入华北很荒唐，因为从地域、地理、经济等方面来看，甘肃都跟华北比较远，应该划入西北或者西部。<br />
<br />
目前我国地理教材对大区域的划分主要有三种方式：一种是按照自然地理划分，一般分为北方地区、南方地区、西南地区、西北地区等;第二种按照行政区划划分，分为华北地区、西北地区、东北地区、西南地区等;第三种是按经济划分，分为东部地区、中部地区、西部地区等。<br />
<br />
“中图版”教材1995年编写<br />
<br />
从1995年秋季起，北京市部分区县的三年制初级中学开始使用由北京市组织编写的九年义务教育初中地理教材。这套教材是由北京教育学院宣武分院二部和北京市教育局教学研究部合作编写的，由中国地图出版社出版，包括教科书、填充图册、教学参考书和陆续配套的电教教材。 <br />
<br />
新闻回顾：Wen Jiabao与北京35中师生一起连上五节课（视频） <br />
<br />
新闻摘要：9月4日上午，Wen Jiabao来到北京市第35中学，在初二(五)班和学生坐在一起连上5节课。Wen Jiabao说，国家从今年起对义务教育阶段教师实行绩效工资，保证教师平均工资水平不低于当地公务员平均工资水平。 (本文来源：新华网 作者：董娟 邓兴军)</div>

							
							
							
							
