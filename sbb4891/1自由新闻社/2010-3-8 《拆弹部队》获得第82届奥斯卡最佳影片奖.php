本人也许缺乏艺术细胞，从未从《野战排》《现代启示录》这类号称多么具有艺术性的电影中看出什么艺术价值来，但还算熟悉越战历史，对奥利弗.斯通这号小乔姆斯基式的混账深恶痛绝，看到很多不了解越战和越战复杂背景的人说通过《野战排》去了解历史，真是让人叹息。<br />
