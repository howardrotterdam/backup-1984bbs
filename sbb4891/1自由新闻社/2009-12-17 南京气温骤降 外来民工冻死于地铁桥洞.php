　　但是警察除了从死者身份证上发现了死者年龄、姓名和家庭住址外，一时难以再寻找到其他有利线索，便将老人遗物收拾后放上警车带回，同时还把两位和死者相邻而睡、发现死者并报警的民工带回了派出所调查取证。至于死者是否如围观市民猜测冻死、病死等说法，派出所方面答复这个不能妄加推测，具体死因还需法医鉴定后再做结论。<br />
　　<br />
　　■同伴：“老者”死处无人敢睡<br />
　　<br />
　　昨天晚上6点30分，记者再次赶到事发现场，发现上午报警的民工沈师傅已经回到桥下，正在拿着扫帚清扫死者睡过的地方。沈师傅告诉记者，原来睡在3号和4号桥墩下的20多位民工，都被死人吓跑了，自己睡在隔壁一个桥洞下，心里也是发毛，所以想把这个地方扫扫干净去去晦气。<br />
　　<br />
　　讲起如何发现老周的死亡？沈师傅说，早上10点不到，被冻醒后的沈师傅和睡在他旁边的孙师傅，开始喝酒取暖，孙师傅告诉沈师傅，昨晚发现老周脸色不太对，好像也没有吃晚饭就睡了，孙师傅还开玩笑地说，老周别活不过夜里啊！？两人突然觉得应该看看老周是否好了一些。于是，就起身到了老周所睡的床铺，发现老周脸色苍白，双目紧闭，口却大张着，觉得情况不对，就喊了老周几声，见始终没有回应，就用手试探了一下，发现老周已经没有了呼吸，身体也早已冰凉。被吓坏的孙师傅连忙喊沈师傅打电话报警。<br />
　　<br />
　　■担忧：这里还有一个“等死”的<br />
　　<br />
　　昨天中午接近12点的时候，记者采访临近结束，一位年龄20多岁的汤姓小伙子喊住了记者，他说2号桥墩下还有一个绰号“大傻”民工，已经病了20多天，如果再不及时救治，也只能等死了！<br />
　　<br />
　　记者随后来到“大傻”所睡的位置，发现这位绰号“大傻”的民工并没有睡着，见到记者也是两眼迷茫，后来他告诉记者，自己名叫王震学，今年46岁，安徽阜阳人，父母过世，只剩下自己孤身一人在外打工求生活，由于腿疼、肺部也有毛病，所以干不了重活，20天前得了重感冒，一直以来发烧不止，后来是一位在附近卖烤红薯的老师傅给他买了一瓶安乃近。和王震学睡在一起的小汤说，王震学已经20多天没有起过床，这几天也是基本吃不下饭，我真怕再不将他送进医院，他也只有等死了。</div>

							
							
							
							
