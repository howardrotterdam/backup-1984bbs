 来源：中国江苏网 2009年12月17日<br />
<img src="http://i50.tinypic.com/2ztesrn.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　心酸！气温骤降 外来民工冻死南京地铁桥洞<br />
<br />
&nbsp; &nbsp; 中国江苏网报道：12月17日早上，南京一位市民路过安德门地铁站137路公交站台的时候，在桥洞下面看见了一个外来民工冻死在里面。现场有市民正在打110报警，众多群众围观。<br />
<br />
　　据死者的民工朋友介绍，死者姓周，五十岁左右，来自泗洪。本是从老家到南京来打工的，但是因为现在工作不好找，挣不到钱，他有时就靠要饭为生，常住在桥洞下过夜。<br />
<br />
　　“我一看他这个样子肯定不行了。我一摸冰凉冰凉的。”今天早上起来的时候，一个民工师傅去买早点，经过这个地方，发现他脸色不对，嘴涨得很大。最后手去摸摸冰凉的。估计是冻死的。<br />
<br />
　　死者的民工朋友表示：“睡桥洞底下的人多了！今天估计二三十人吧！现在住旅店太贵唉，每天就这点钱够吃个饭，一住就没钱了！你住一晚旅馆（的钱）能够吃饭了。”<br />
<br />
　　记者在现场看到南京安德门137路公交站旁边的桥洞下住着不少民工。那边环境脏、乱不堪。看见自己的同乡客死街头，不少务工者都表示现在城市给他们打工的机会实在少，一时找不到工作又住不起旅店，只能睡在露天。天这么冷有时候晚上几个人要挤在一起取暖。其中一位民工说，这边还有个民工干活时腿受伤了，20多天前就一直躺在这里没人管。要是再不救治估计下场比周某也好不到哪去。<br />
<br />
　　近日，南京天气温度持续走低，夜间都将在0度以下，加上湿度比较大，穿上厚厚的棉袄人都觉得冻得受不了。外来民工为了混口饭吃，他们却没有温暖，只能在这个城市里面过着受冻的日子。南京大大小小的立交桥很多，在桥洞里面过夜的人很多。希望南京市相关部门妥善处理，避免悲剧的再次发生。（特约记者 周华）</div>

							
							
							
							
