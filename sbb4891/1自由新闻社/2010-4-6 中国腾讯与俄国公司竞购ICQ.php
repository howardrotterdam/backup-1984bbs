													<h2>中国腾讯与俄国公司竞购ICQ</h2>
						
						
																											<div id="postmessage_551882" class="t_msgfont">【俄新网／莫斯科4月6日电】 <br />
今天出版的俄罗斯《新闻报》报导说，俄罗斯Digital Sky Technologies (DST)、Prof Media和中国腾讯公司（Tencent）递交了收购网络实时通讯公司ICQ的申请。 <br />
<br />
《新闻报》表示，美国在线（AOL）已经收到了有关ICQ的收购要约。这一消息得到了DTS和Prof Media公司消息人士的正式。据悉，除这两家俄罗斯公司外，中国腾讯公司也递交了收购要约。<br />
<br />
《新闻报》表示，当前尚不清楚何时公布竞标结果。DTS和Prof Media公司发言人拒绝对此发表评论，美国在线和腾讯没有回答《新闻报》的提问。<br />
<br />
去年12月，美国在线从时代华纳公司（Time Warner）中剥离，并开端出售非主业资产。其中包含ICQ，美国在线对其估价3亿美元。<br />
<br />
据消息人士表示，Prof Media提出的收购价格为1.2亿美元。他说，DTS的出价要高一些。据《华尔街日报》报导，DTS可能出价2亿至2.5亿美元。<br />
<br />
消息人士表示，这一交易将为DTS和Prof Media分别控制的Mail.ru和Rambler两家站点带来ICQ的用户。Mail.ru的实时通讯软件<a href="mailto:Agent@Mail.ru">Agent@Mail.ru</a>有1200万用户，ICQ是其在俄罗斯的主要竞争者。<br />
<br />
Rambler从2005年起成为ICQ的协作伙伴，共同在俄罗斯推广ICQ俄文版--Rambler的ICQ当前有500万用户。同时，ICQ在俄罗斯还同YANDEX进行协作，其用户有400万。<br />
<br />
腾讯公司拥有在中国最为流行的实时通讯软件QQ。<br />
<br />
《新闻报》表示，使用ICQ的大部分是俄语用户。ICQ首席运行官3月份表示，ICQ的4200万用户中，大约有44%，即1850万为俄语用户。消息人士表示，美国在线邀请了多家俄罗斯公司参与竞标，包含YANDEX，但YANDEX拒绝了这一建议。<br />
<br />
ICQ的主要收入来广告。美国在线没有公开ICQ的财务指针。消息人士表示，Rambler站点2008年依靠ICQ广告获得了600万美元收入，2009年的情况类似。<br />
<br />
【2010/04/06 俄新网】</div>

							
							
							
							
