													<h2>民主党一向是个贸易至上的政党</h2>
						
						
																											<div id="postmessage_466860" class="t_msgfont">美国加大对中国政治压力<br />
英国《金融时报》 詹姆斯・波利提, 丹尼尔・董贝 , 华盛顿报道 2010-02-04<br />
<br />
美国昨日加大对中国的政治压力。美国总统巴拉克・奥巴马(Barack Obama)提起人民币汇率偏低的问题，而美国参议院前一天谴责了针对谷歌(Google)的网络攻击。<br />
<br />
奥巴马昨日对参议院民主党议员发表讲话时警告，对于与中国开展贸易，美国不应打退堂鼓。<br />
<br />
“我们如果把自己锁在那个市场以外，将是一个错误，”他表示。但他补充说，汇率问题需要在国际层面得到解决，“以确保我们的产品不会被人为抬高价格，而他们的产品不会被人为压低价格”。<br />
<br />
华盛顿与北京之间的紧张态势近日加剧，此前奥巴马政府批准了64亿美元对台湾军售方案，而奥巴马本月预期还将与西藏精神领袖达赖喇嘛会面。<br />
<br />
美国参议院周二一致通过相关议案，谴责针对谷歌和其它公司的网络攻击，这些攻击导致搜索引擎集团谷歌威胁将退出中国。<br />
<br />
“重要的是，在中国侵犯信息自由流动之际，美国不能袖手旁观，”宾夕法尼亚州民主党参议员阿伦・斯佩克特(Arlen Specter)表示。<br />
<br />
民主党一名资深参议员还要求30家西方大型科技集团详尽汇报各自在中国的人权实践。<br />
<br />
伊利诺伊州民主党参议员、参议院多数党二号人物迪克・德宾(Dick Durbin)发出了一份措辞严厉的信息索取函件，收件人包括Facebook、苹果(Apple)、IBM、亚马逊(Amazon)和甲骨文(Oracle)等美国集团，也有诺基亚(Nokia)、沃达丰(Vodafone)、SAP、RIM和东芝(Toshiba)等海外公司。<br />
<br />
德宾表示，谷歌“在站出来反抗中国政府长期不尊重言论自由和隐私权等基本人权的问题上，树立了一个强有力的榜样。我期盼着了解其它……公司是否愿意跟随谷歌这个榜样”。<br />
<br />
德宾称，他期望这些公司在回复中介绍其中国业务，以及它们是否作出了努力，以确保自己的产品与服务没有“助长中国政府侵犯人权的行为”。<br />
<br />
美国国会中国问题执行委员会下周将举行一个听证会，聚焦美国企业受到的网络攻击及其引发的知识产权保护问题。该委员会是在2000年中国加入世界贸易组织(WTO)之际成立的。<br />
<br />
美国国家情报总监丹尼斯・布莱尔(Dennis Blair)本周在国会作证时，强调指出中国的“国际信心和活跃程度与日俱增”。<br />
<br />
但他补充说，北京方面的政策主要出于商业利益，而其“核心重点仍是确保国内稳定”。<br />
<br />
布莱尔在作证时，将针对美国的全球“网络威胁”列在华盛顿面对的所有威胁之首。他形容谷歌近期受到的网络攻击是“一记警钟，唤醒那些迄今不重视这个问题的人”。<br />
