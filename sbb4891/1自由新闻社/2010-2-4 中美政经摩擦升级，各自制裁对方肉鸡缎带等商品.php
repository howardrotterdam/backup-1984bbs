2010年 2月 4日 星期四 15:53 <br />
 作者 周昕/魏伦<br />
<br />
路透北京2月4日电---美国总统奥巴马周三对人民币汇率问题表现出更为强硬的态度,中国政府研究人员对此反应冷淡.美国认为人民币汇率被人为低估.<br />
<br />
中国政府未对此迅速做出官方评论,但商务部国际贸易经济合作研究院研究员李健称,中国政府不会迫于美国的压力而改变现有汇率政策.<br />
<br />
&quot;即便中国要调整汇率,也不会按照美国方面的要求,说要升值多少多少,这是中国的自主决策.&quot;李健对路透表示.<br />
<br />
他认为,人民币汇率在2010年会保持相对稳定.<br />
<br />
&quot;人民币不会出现突然的大幅度的升值,&quot;李健说,&quot;在汇率问题上,中国首要考虑的是自身经济的稳定增长及经济结构调整.而不是外国的压力.&quot;<br />
<br />
奥巴马周三在一次民主党聚会上发表讲话称,&quot;我们必须解决的一项国际挑战是汇率问题,以及如何调整汇率以确保美国商品价格不会被人为抬高,而亚洲或中国产品价格不会被人为压低,这令美国商品处于巨大的竞争劣势.&quot;<br />
<br />
中国国家发改委宏观经济研究院的研究员左传长表示,奥巴马的强硬态度只是为了取悦美国国内选民,而非真正要对北京施压.<br />
<br />
&quot;这只是政治说词而已,并没有太大的实际意义,&quot;左传长并称这是其个人看法.与李健一样,他认为汇率政策是&quot;主权问题&quot;,中国不会屈从于国外压力.<br />
<br />
&quot;对,中国可以在某些国际问题上做出一些让步,但你不能强迫中国做事.&quot;左传长说.<br />
<br />
**升值步伐缓慢**<br />
<br />
中国于2005年7月允许人民币兑美元升值2.1%,在随後的三年中进一步令其升值19%.但在2008年7月停止升值,以帮助国内出口企业应对全球经济危机.<br />
<br />
多数经济学家预计,中国将在今年再次允许人民币温和升值,目前出口已经恢复同比增长,经济维持两位数增幅,且通胀压力正在上升.<br />
<br />
强势汇率将可抑制通胀,并刺激企业为国内市场生产,而非针对出口.<br />
<br />
但市场并不指望人民币能迅速升值.<br />
<br />
上海的一位美资银行交易员表示,&quot;美国政府此前有关人民币的强硬措辞并未产生任何效果.市场尚不确定,奥巴马的最新言论真地表明美国对人民币的立场更趋强硬.&quot;<br />
<br />
人民币/美元NDF一年期报价周四报价显示,未来12个月中人民币将升值2.8%,略低于周三.人民币现汇则几无变动.<br />
<br />
北京一家国有银行交易员称,&quot;中国不太可能就美国的压力做出大幅让步,尤其是目前两国之间出现了一系列争端,包括美国对台军售.&quot;<br />
<br />
该交易员并称,&quot;若美国对人民币汇率的立场更为强硬,或将反而无法达到预期目标.&quot;<br />
<br />
**&quot;升值要求未免滑稽&quot;**<br />
<br />
人民日报援引中国银行分析师方明称,那些要求人民币升值的人忽略了人民币自2005年来已显着升值的事实.<br />
<br />
方明说,&quot;一些国家领导人和所谓知名学者竟然异口同声地要求人民币升值,未免有些滑稽.&quot;<br />
<br />
他的看法与中国政府的态度一致,即中国及时采取刺激措施确保了经济增长,已经为全球经济做出很大贡献.<br />
<br />
中国学者认为,奥巴马说服中国政府允许人民币升值的作用不大,且汇率问题不会导致双边关系出现危机.<br />
<br />
李健表示,两国贸易将会继续繁荣,&quot;中国和美国都能从贸易中获得巨大利益,这仍是双方主流看法.&quot;<br />
<br />
左传长亦同意,人民币升值争端不会引发贸易战,&quot;中美出现些摩擦非常正常,但这不意味着双方会撕破脸.&quot;(完)<br />
