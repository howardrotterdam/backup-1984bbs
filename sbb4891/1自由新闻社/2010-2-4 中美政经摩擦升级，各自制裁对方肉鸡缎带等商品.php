													<h2>中美政经摩擦升级，各自制裁对方肉鸡缎带等商品</h2>
						
						
																											<div id="postmessage_466027" class="t_msgfont">核心提示：2月3日，美国总统奥巴马发表最新讲话表示，称必须处理汇率问题，以确保美国产品不会面对“庞大的竞争不利因素”。奥巴马强调会对中国采取较强硬措施，确保对方遵守贸易法则。中美两国最近在对台军售、达赖问题、汇率以及互联网问题方面展开全面交锋。<br />
<br />
人民网2月4日报道 据香港文汇报消息，美国总统奥巴马昨日发表最新讲话表示，中国和亚洲将会继续是美国出口的庞大市场，但必须处理汇率问题，以确保美国产品不会面对“庞大的竞争不利因素”。<br />
<br />
奥巴马向民主党参议员表示，我们必须处理的其中一个国际挑战，是汇率问题，以确保我们的货品价格不会人为地被推高，而他们的货品价格被人为地压低。<br />
<br />
奥巴马还说，会对中国采取较强硬措施，确保对方遵守贸易法则;不过，他同时强调，中止双方已经签订的商业协议，美国将蒙受损失。<br />
<br />
中美近期就四大问题展开交锋：<br />
<br />
1、军售问题 <br />
<br />
美国将启新一轮对台军售<br />
<br />
美联社1月26日援引美国资深国会议员的话称，奥巴马政府将宣布新一轮对台军售，这轮军售可能包括UH-60黑鹰直升机、“爱国者-3”型导弹等。<br />
<br />
外媒:中国强硬回击对台军售令美方意外<br />
<br />
美国宣布向台湾出售总额近64亿美元的武器装备后，中国外交部、国防部、国台办、全国人大外事委员会等部门纷纷向美国提出抗议和交涉。对此，外媒评述说，美国没有想到中国会做出这么激烈的反应，“不希望对美中关系造成永久伤害。”<br />
<br />
2、达赖问题<br />
<br />
中方多次警告后奥巴马仍按计划见达赖<br />
<br />
白宫发言人伯顿2月2日称，“奥巴马总统去年访华时曾透露，他将会与达赖喇嘛会面，目前他正准备这样做。”<br />
<br />
中国外交部火速回应 坚决反对达赖访美<br />
<br />
外交部发言人马朝旭2月3日就美方称美国总统奥巴马将会见达赖回答记者提问。他说，中方坚决反对达赖去美国窜访,坚决反对美国领导人以任何名义和形式与达赖接触。这一立场是一贯的、明确的。<br />
<br />
3、汇率问题<br />
<br />
奥巴马称将强硬对待汇率问题<br />
<br />
2月3日，美国总统奥巴马发表最新讲话表示，称必须处理汇率问题，以确保美国产品不会面对“庞大的竞争不利因素”。奥巴马强调会对中国采取较强硬措施，确保对方遵守贸易法则。<br />
<br />
外交部:中国从未利用所谓汇率操纵获利<br />
<br />
2009年5月14日，外交部发言人马朝旭举行例行记者会时称，中国政府从来没有利用所谓汇率操纵来从国际贸易中获利。在汇率问题上对中国进行毫无根据的指责只会助长美国的贸易保护主义情绪，这对于真正找到解决问题的方法没有任何帮助。<br />
<br />
4、互联网问题<br />
<br />
希拉里指责中国互联网政策 外交部坚决反对<br />
<br />
美国国务卿克林顿1月21日就“互联网自由”问题发表演讲，其中谈到中国互联网管理政策，外交部在答问中称对于这种违背事实、损害中美关系的言行，我们坚决反对。<br />
<br />
