<br />
今年是“文革”四十周年，“六三六五”十七周年。文革和六三六五的关系，可以用一句话说清楚：都是中国特色，都是一党专政的产物。在现代文明社会里，在民主制度下，文革和六三六五都是不可思议的。即使出十个毛泽东，二十个邓小平，也形不成什么气候，搞不成什么文革和六三六五。但是，只要存在着共产党领导一切的制度，即使毛泽东死了，邓小平也死了，文革和六三六五仍然存在着不断重演的现实的危险。没有大文革，还会有小文革，没有大六三六五，还会有小六三六五，不出在北京天安门，照样会出在什么省的什么地方，比方说，广东省的汕尾市。文革和六三六五的根源是一党专政这个制度，毛和邓只是扮演了其中最重要的一个角色而已。<br />
<br />
毛泽东自己说，他一辈子干了两件事，打败了蒋介石，搞了文化大革命。所谓打败蒋介石，就是以暴易暴，以大暴易小暴，作为暴君，毛泽东确实达到了自己的目的，超过了秦始皇。所谓文化大革命，就是发动了一场长达十年连绵不断的大斗争，“八亿人民，不斗行吗”，“谁反对毛主席就打倒谁”，他屡战屡胜，又达到了目的，超过了斯大林。这两件事情，毛都大获全胜。至于功过，有的历史学家把他分为三段，说他“开国有功，建国有过，文革有罪”。我刚才说了，一党专政是文革的根源。因此，从开国到文革，是从因到果的关系，不是从功到过的关系。建国饿死了几千万人，也不是“有过”二字概括得了的。毛泽东这个反人类的罪人，不值得由人类来歌颂。<br />
<br />
邓小平不是理论家。不存在什么“邓小平理论”。认为谁当了共产党的头，就一定精通马列主义，掌握真理，创造什么“理论”，那纯粹是迷信。毛泽东在世时，邓小平是个惟命是从的执行者，毛叫邓干什么，邓就干什么。邓的全部本钱，不是理论，而是力量。共产党有七个领袖，毛刘周朱林五个都死了，陈云身体衰弱，剩下一个邓小平，从华国锋手里接收了共产党的全部资源。胡耀邦赵紫阳都没有实力，但他们二位，认准目标，义无反顾，竭尽全力，推动中国进步。邓小平的实力其大无比，但是如此宝贵的力量，却被邓小平用到“四个坚持”上去，开辟了一条在一党专政下武装镇压人民和瓜分国有财产的腐败之路，造就了今天如此糟糕的人权纪录，如此悬殊的贫富鸿沟。这是什么？这是倒退，这是犯罪！邓小平完全有力量推动中国转向民主社会，很可惜，他不惜倒退，不惜犯罪，自愿与一党专政共存亡，堕落为千古罪人。<br />
<br />
毛泽东和邓小平都留下了巨大的“负资产”。他们用实践证明了毛式社会主义和共产党领导一切的破产。他们给后人留下了否定他们的机会。否定毛式社会主义，否定一党专政，中国就能进步。<br />
<br />
至于现在的新领导人是不是正在朝这个方向努力？这一点，我看不出来。我不知道。我很希望也很愿意欢迎他们能够真的也朝着这个方向努力，但是我现在看不出来。我有把握的，我已经看清楚的是：民众的维权行动正在推动中国朝这个正确的方向前进。<br />
<br />
我指的是公民大众维护自己权益的行动。由于干部侵犯公民权益的现象愈来愈肆无忌惮，愈来愈普遍，公民维护自己权益的行动也就相应地愈来愈发展，风起云涌，赢得愈来愈广泛的社会同情。九十年代，每年一两万起。去年2005年，发展到八万多起，平均每个县四十起，平均每五分钟一起，每起几十人，几百人，几千人不等，这八万多起是专指群体性的维权行为，个体性的行为没有统计在内。我看，中国已经到了民众维权的时代。这是逼出来的，是一党制把公民逼上了维权之路。全世界都是这样走过来的。中国的现代化，也同样要靠维权来实现。腐败和黑暗的制度性根源，只能在民众维权过程中铲除。公平，理性，自由，活力，只有通过维权才能发扬。维权的过程，就是确立民主政体，建设法治国家，形成文明社会的过程。中国将依靠民众维权而跟过去告别。<br />
<br />
当局应该执行宪法！一切合法的政府和团体都应该执行宪法！《宪法》第二条说得非常好：“中华人民共和国的一切权力属于人民。” 宪法是根本法，这一条，说的就是国家权力的根本。人民的权力遭到侵犯，意味着国家的法律遭到威胁；人民的权力经常而普遍地遭到践踏，意味着国家正在变质，或者面临着变质的危险。所以，维护公民权，就是护法，护宪，护国。共产党当年支持二七大罢工，不就是支持工人维护集会结社和游行示威之权吗？支持一二九，不就是支持学生“指点江山激扬文字”的言论自由吗？支持农民运动，不就是支持“二五减租”和“耕者有其田”吗？中国共产党是靠支持公民维权才发展壮大起来的。今天在新的历史条件下，依法治国，建设法治国家，更应该通过维护公民权利，来护法，护宪，护国。这是共产党最重大的责任，也是共产党最好的防腐剂。凡是没有蜕化变质的共产党员，都不应该忘本，都应该义不容辞，见义勇为。总而言之，应该和被侮辱和损害的民众在一起。这不是什么新话，六十多年以前，整个社会就是这样教训我们的，我当时认为很对，接受了，今天我仍然持这个观点，没有变。<br />
