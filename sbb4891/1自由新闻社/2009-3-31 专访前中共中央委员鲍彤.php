													<h2>专访前中共中央委员鲍彤</h2>
						
						
																											<div id="postmessage_65547" class="t_msgfont">美国之音记者张楠与何宗安最近在北京对前中共中央委员、政治局常委的政治秘书兼政治改革委员会主任鲍彤做了电视采访。鲍彤谈到了从1989年天安门民主运动平反，到美国总统奥巴马的就职演说等一系列问题。中文部电视组据此制作了多段纪录片。<br />
<br />
采访鲍彤记录稿<br />
<br />
张楠：我们知道六三六五之后，您被判刑七年，七年之后您获释，又被剥夺政治权利两年。<br />
<br />
鲍彤：名义上是两年。<br />
<br />
张楠：那么现在您是不是已经完全恢复了自由？<br />
<br />
鲍彤：我想，我只能讲我已经适应了。<br />
<br />
张楠：适应什么了？<br />
<br />
鲍彤：适应了我现在的处境。至于恢复自由，那么我相信，中国的老百姓没有一个人得到过，或者正在享受着中国宪法所规定的自由。<br />
<br />
比方说言论自由。符合组织原则的话可以说，被鼓励说，不符合组织原则的话是不准说的，或者说，说了以后会使你感到恐惧的。<br />
<br />
集会结社的自由，我相信中国老百姓是不存在的。当然，共产党开会是用不着到民政部或者公安局去登记。但是，老百姓要是集会结社的话，当局认为这是针对他们的话，如果他认为这是一种敌我矛盾的话，那他可以随时取缔，而名称就是，你没有向有关当局申请，也没有经有关当局批准。<br />
<br />
至于信仰自由，我想中国的信仰自由实际上只是在表面上，他要求你信仰共产党所信仰的一切，而不喜欢你信仰共产党所不喜欢的一切。不管是基督教，伊斯兰教或者藏传佛教，实际上，没有信仰自由可言。<br />
<br />
也就是说，有信仰共产党主旋律的自由，没有信仰非主旋律的自由。如果说中国人都不普遍存在充分的自由，那么我的不充分的自由也应该适应。<br />
<br />
张楠：那您出入啊或去其他地方是不是都允许的？<br />
<br />
鲍彤：有允许的时候，大部分是允许的，但是允许的情况下，我是在被监控的情况下允许的。<br />
<br />
也就是说，我即使跑到门口，去买一瓶矿泉水，也有人盯着我。有一辆汽车，一辆摩托车，再加上若干个步行的人一起跟着我。为什么我去买一瓶矿泉水需要有人跟着呢？我想无非是对我不放心，因为万一我出去后，我说：&quot;Taxi&quot; ，那时候，汽车马上就要跟上来，所以汽车会随时出动。<br />
<br />
那么为什么还有摩托车呢？因为，汽车跟我的时候碰上红灯是，我过去了它(指汽车)没过去，他的摩托车可以非常灵活的抢过红灯，它是有当局发给的证件的，不怕闯红灯。<br />
<br />
因此，即使我只走一个街区，也有步行的，汽车，摩托车跟着我。我是想说明的，既使是晚上12点出去，也有人值班。我离开我家门的时候，他们就得到消息，因此，我走到门口的时候，他们也从别的地方走到门口跟我会合，然后，一起走。<br />
<br />
张楠：刚才我们开车，也就是从我们美国之音的北京分社一直开过来，经过了长安街，经过了天安门，这段路就是当年解放军杀进北京的那条路线。我也发现您所住的这个地方，离当时开枪的地点非常近，而且附近不远处也有复兴医院。复兴医院当时接收了大批死伤的市民和学生，这是当时的情况。那么回想当年的情况，我想知道，天安门发生镇压的时候，您在什么地方？<br />
<br />
鲍彤：我在监狱里。<br />
<br />
张楠：噢，那个时候您已经在监狱了？<br />
<br />
鲍彤：我从5月28号就在监狱里。没有说逮捕我，说是监视居住。因此我是在监狱里被监视居住的。我原来以为监视居住就是我住在什么地方，有人来到我居住的地方监视我，我以为是这样，而不是把我放在监狱里来居住。<br />
<br />
因此我知道，监视居住和坐牢在中国实际上没有任何区别。他的区别只是可以不履行法律规定的任何手续，不经过任何合法机关的批准，没有任何的法律有效文件，就可以宣布你是监视居住的对象，而且要在他需要监视的地点。<br />
<br />
张楠：是秦城监狱吗？<br />
<br />
鲍彤：秦城监狱。<br />
<br />
张楠：那您是怎么知道发生六三六五镇压的？<br />
<br />
鲍彤：大概是在6月8号或9号的时候，因为从6月4号以后，我没有得到人民日报。当时在5月28号坐牢后，天天可以看人民日报，但是6月4号以后，就不给我报纸了，我天天要天天不给，大概到了6月8号或9号的时候，给我报纸了，我才知道天安门发生了一个大悲剧。<br />
<br />
张楠：您到那个时候才知道发生了什么事情，可是，您当时是一名共产党员，那么看到共产党所领导的子弟兵向人民开枪，不知道您当时是一种什么心情？做何感想？<br />
<br />
鲍彤：我当时是共产党的一个中央委员，而且是主管政治体制改革的中央委员，也是参与了解中共最高层决策的一个中央委员。因为我当时的身份是，除了中央委员以外，我还是中央政治局常委的政治秘书，我还是中共中央政治改革委员会的主任，中共中央宣传小组的成员，中共中央党建小组的成员。<br />
<br />
所以当学生学潮开始的时候，了解学潮的情况，这是我的职责的一部分，我是在这种情况下到秦城监狱的。到秦城监狱的时候没有一个人说要逮捕我，只是说政治局常委要开会，要我参加。<br />
<br />
因此，我跑到中南海，一到中南海，有一个政治局委员跟我聊了几句天儿，聊了几句非常一般的天儿。<br />
(他问)：“ 最近在忙什么”？<br />
我说：“我最近很忧虑”。<br />
“噢，什么事情啊”？<br />
我说：“我不知道学潮现在处在这样一种僵持状态到底应该怎么解决”？<br />
“噢，那你现在安全不安全啊”？<br />
我说：“我没有觉得有什么不安全的”。<br />
“你是不是应该找个地方，安全一点，保护一下，因为现在很多学生要找你”。<br />
我说：“没有，没有学生找我”。<br />
“不，你还是找一个地方安全一点儿”。<br />
<br />
我说：“我的家就住在复兴医院的边上，当时这个地方有一个楼，叫24号楼，是中共中央和国务院的部长的宿舍。我当时就住在部长楼里。当然后来我出狱后，就把我扫地出门了，送到 “八宝山”去了。我这个地方没有什么不安全的”。<br />
<br />
当时所有的部长都住在这里，我的办公室在中南海，在中南海的勤政殿，也就是中共中央常委办事机构的地方。5个常委有5个办公室，我作为常委的秘书，我也有个办公室，也在那个地方。<br />
<br />
我说：“这是最安全的地方，我晚上不回家，就在勤政殿住，没有必要嘛”。<br />
他说：“不，恐怕还是考虑考虑住到一个什么地方去好”。<br />
我说：“谢谢你的关心，我觉得没有这个必要”。<br />
<br />
我出来时我发现我自己的车没了，换了一辆车，这辆车我看到的号码是：GA 公安。左边一个人，右边一个人，告诉我，就是这个车。我心里知道，那就是公安部专门运送囚犯的车。<br />
<br />
我找谁呢？一个人都不认识，送我出来的人是一个政治局委员，他跟我热烈握手，走的时候跟我热烈握手，手握的非常紧，当时没有任何预兆。左边一个人，右边一个人，说：走吧。我就进去了，我进去时是在后排坐着，他说，请你再进(去)一点，那么这边上来一个人，右边上来一个人，左边上来一个人，后边上来一个人，前边有两个人，一个是司机，(另)一个人拿着对讲电话，开出去了。<br />
<br />
当时街上到处都是学生，市民。 有的在游行，有的在发表演讲。车走动哪里不通，就拐个弯儿，再走，不通就拐个弯儿，再走。因此，我已经糊涂了，闹不清是什么方向了。最后，显然是到了郊区，看到了山，进了两道铁门，我下来，有三个人站在车边上，迎接我。<br />
<br />
我下来后就问，这里是不是秦城？这三位迎接我的人说：“是”。第一个说：我是监狱长，他指着第二个人说，这是监管处处长，指着第三个人说，这是监管科科长。<br />
<br />
这时，送我来的车，回头就走了。我注意到，我出来的时候，我的车后边的情况我不知道，前面有一辆车开道的，也是GA， 也是公安，他走到哪里，我们就跟到哪里。一到秦城，回过头一看，两辆车都走了。<br />
<br />
那么，我就在秦城住下来了，在秦城住下来没有任何手续，也就是说，我进监狱，没有任何法律文件。我问他们是不是要找我谈什么事情？监狱长，监管处长，监管科长说，不会有什么事情。那么我说，“我很疲劳，我想睡觉”。“可以”。<br />
<br />
我进了我的地方，我那个地方有个小花园，那个地方有个房子，大概有10个平方米的样子。为什么说10个平方米呢，因为除了一张床，就没有东西了。所谓床呢，就是两条长板凳上面一块木板。那是夏天5月28号，我很累，躺在床上我就睡着了。这就是我到秦城的时候情况。<br />
<br />
至于后来开枪6月3号我不知道，4号也不知道，到了8号，9号我才看到报纸，说是“平暴“，平定了暴乱。<br />
<br />
接着我就知道了，到了6月30号，有一个人叫陈希同，当时他是北京市长，他代表国务院中国人大常委，也就(等于)是美国的议会作报告，关于平定学生暴乱的报告，其中提到我的名字，说我有多项罪状。这个时候我才知道，原来被平定了，那一场学潮。如果说邓小平一开始称它为动乱，那到这个时候已经成为暴乱了。<br />
<br />
张楠：那么现在回过头来看，当时6.4采取这种血腥镇压的方法，你觉得给中共，给中国共产党带来什么？有什么样的后果？<br />
<br />
鲍彤：我认为，他给全世界的共产党丢了脸，马克思都会在九泉之下感到惭愧，感到内疚。<br />
<br />
张楠：那么这些年来，人们一直在呼吁给六三六五平反，您认为平反六三六五对中国来说意味着什么？有什么样的意义？<br />
<br />
鲍彤：我认为，如果六三六五能够平反，就意味着中国能够得到新的生命，意味着中国能得到新生，意味着中国人民有自己的人权。因为六三六五本身就意味着中国老百姓是没有人权的，而只有中国的领导人是有践踏人权的特权的，是有拿国防军来对付学生，来对付市民的特权。<br />
<br />
张楠：那么现在来平反六三六五的话，据您看会不会引发动乱和社会不稳定？采取一个什么样的措施来平反六三六五会比较稳妥一些？<br />
<br />
鲍彤：我想，如果六三六五能够平反，那么这就是中国建设和谐社会的开始。即中国真正成为和谐社会的起点，我认为这是不值得害怕的，即使对共产党来说也不值得害怕。<br />
<br />
如果共产党能够平反六三六五，那么共产党就建立了超越毛泽东，超越邓小平的功勋，也就是说使共产党从人民的主人，真正开始变成一个人民的代言人，或代言职业。那他就能得到新的生命，会得到老百姓的拥护。<br />
<br />
张楠：那您对最终给六三六五平反是不是抱有信心呢？<br />
<br />
鲍彤：我认为，如果要讲老百姓，老百姓从来没有认为镇压六三六五是正确的。在六三六五以后，我所接触的人，不管是40年以前，还是50年以前，60年以前的朋友，见了我都非常亲切。这种感觉使我知道，在老百姓的心里对六三六五的看法跟官方的书上所宣传的关于六三六五的说法根本不是一回事。<br />
<br />
张楠：那么有人说现在中国的改革开放取得了伟大的成果，这说明1989年中国动用军队对付天安门的民主运动是必要的和正确的，对于这样一种说法，不知道您有什么样的评论？<br />
<br />
鲍彤：我想，我可以非常冷静的说，这是一种希特勒的哲学，这是希特勒都不敢说的哲学。有人说，中国今天的繁荣是当年的镇压造成的，那么请问，如果这是一条真理的话，那么为了中国继续繁荣，难道还要继续镇压吗？为了使中国永远繁荣，难道要永远来镇压中国老百姓的言论、集会、结社的自由吗？<br />
<br />
我说，这种逻辑，只能说是希特勒的逻辑。我想我已经用最温和的话，我不想用强盗逻辑之类的话。<br />
<br />
张楠：我们知道，六三六五镇压据说是邓小平亲自下令进行的，造成至少数百人的死亡。 那么我们也看到邓小平是中国改革开放的一个先驱人物和领导者，对中国今天的繁荣也作出了贡献。那么对邓小平这个人，您怎么评价？<br />
<br />
鲍彤： 我想，我这样来评价邓小平，邓小平是中国共产党的化身，邓小平确实是中国80年代经济改革的最有力量的支持者，其所以支持中国的经济改革，用他自己的话来说，不改革就是死路一条。很多人认为这个死路一条是指中国人死路一条，其实，错了，应该说是共产党死路一条。<br />
<br />
邓小平很清楚，白猫，黑猫，能逮住耗子就是好猫。什么叫逮住耗子，就是保持住共产党的统治地位。如果市场经济可以使共产党保持统治地位的话，那么他可以抛弃计划经济，抛弃列宁，抛弃斯大林，抛弃毛泽东。但是他的中心点是要坚持党的领导地位，统治地位。也就是说，他所谓要抓的那只猫，并不是人民的幸福，并不是人们的权利，而是共产党的统治地位。<br />
<br />
因此，当他认为学生对共产党的领导构成某种威胁的话，不管是真威胁还是假威胁，是真实的情况或者依靠腐败，依靠什么呢？依靠欺骗。依靠什么呢？依靠压住不同的声音。依靠这三种东西来维持自己权力的人，(像)奥巴马总统说的很好，我认为很好，他说：你们站在历史的错误的一方。当然，奥巴马并不认为这样的人是他的敌人，他说：“只要你松开拳头，那么，我也可以向你伸出援助之手。”<br />
<br />
我想，美国新总统的这番话，我认为，我作为一个中国的公民，我觉得能够引起我的共鸣。我也认为当中国共产党动用几十万国防军来对待学生，来对待市民的时候，是站在历史的错误方面。他是为了牵制压制不同意见来维持自己的权力，因此他是站在历史的错误方面。<br />
<br />
但是，如果他能松开自己的拳头，不再坚持说，稳定压倒一切是正确的，平动乱，平暴乱是正确的，那么我想，我作为一个中国的公民，我是可以跟他合作的，我也可以向他伸出我的援助之手，跟他一起来建设中国的和谐社会。<br />
<br />
张楠：接下来，我们来谈一下赵紫阳吧。<br />
<br />
鲍彤：好。<br />
<br />
张楠：六三六五以后，您有没有再见到过赵紫阳？<br />
<br />
鲍彤：没有，我最后一次见到赵紫阳，是在1989年5月，大概是5月26号。也就是说，26号，我还见到过他一次，27号我没有到他家里去，28号我自己就到了秦城。在秦城呆了七年，我当然不可能见赵紫阳，后来我又(被)剥夺政治权利两年，我为了避免当局多心，怀疑，愚昧，我没有跟赵紫阳有什么联系。<br />
<br />
张楠：那么，以后呢？剥夺政治权利两年以后，有没有电话联系或某种(其它)形式？<br />
<br />
鲍彤：我没有跟他打过电话，他也没有跟我打过电话。他确实通过一个朋友，叫宗风鸣到我家里来。宗风鸣，大概很多人知道他，他跟赵紫阳是同年人，都是1919年生的，都是河南人，都是在抗战初期参加共产党，在河南进行抗日游击战争。<br />
<br />
后来紫阳从河南到了广东，宗风鸣先生可能到了贵州或者云南这一带。后来又回来，到了飞机场啊，北京航空学院啊这些地方。我相信他大概在离休以前，是北京航空学院的党委书记，我相信他大概是这样一个职位。他当时经常可以到赵紫阳家里去。所以呢，我出来后，他也可以到我这里来，当时他跟我说过，赵紫阳有个意思，帮他写一本回忆录，叫做：“北京十年”。<br />
<br />
这个意思我知道，因为我在监狱里的时候，紫阳跟我的夫人蒋中曹，那位老太太就说过：“鲍彤判刑了，七年，叫他在监狱里好好休息一下，将来出狱后，来帮我一起来写一本书，叫做： “北京十年”。<br />
<br />
因为赵紫阳1980年到北京，他四月到北京，五月我的身份就是国务院总理的秘书。他当国务院代总理，国务院总理，我是国务院总理秘书，这是五月份的事情。那么，从1980年的5月到1989年的六月，正好是十年，历经十年。<br />
<br />
我跟宗风鸣先生说，我现在的处境不可能从事这样的事情。如果从事这样的事情，会引起当局的极大的顾虑，本来我已经是24小时全天候的监控，紫阳本来就行动不自由，如果他(指当局)知道鲍彤和紫阳有所接触的话，我相信，我的生存空间和赵紫阳的生存空间一定等于零。这样的话，对紫阳没有好处，对我也没有好处，“ 北京十年“也不可能写出来，不仅不能写出来，反而会引起很多复杂的问题。这件事情后来没有再谈。<br />
<br />
张楠：那么，现在是不是具备一定条件可以写了？<br />
<br />
鲍彤：紫阳已经去世了，紫阳本来是要我帮助他写一个 “北京十年”，紫阳已经去世了。<br />
<br />
张楠：那么作为赵紫阳先生生前的一个，叫幕僚也好，叫智囊也好，中共中央政治局常委的政治秘书，那么对这位前中共总书记，您有过密切接触的人，您怎么评价他？<br />
<br />
鲍彤：我在监狱里住了十几天，没有人来找我。后来有人找我，拿着中共中央的一封信，说：“是要对你进行审查，你要好好配合”。我说：“我有什么事情呢？”这个时候(他)就说：“主要不是你的事情，要你回想一下，赵紫阳有什么问题，现在中共中央同时在审查赵紫阳。”告诉我，中共中央在审查赵紫阳，在什么地方，我不知道，也在审查我，我在秦城监狱，以监视居住的名义。<br />
<br />
我就问他：“中共中央为什么要审查赵紫阳”？ 他说：“赵紫阳他犯错误了。”中国有个专案小组对我说，赵紫阳犯错误了。我说， 我认为赵紫阳是一位很难得的，我今天仍然是这样一个评价：赵紫阳是中共中央历史上很难的一位总书记。<br />
<br />
我想在中共中央总书记当中，有三个人是值得后人纪念他们的。<br />
<br />
一位是陈独秀，他是不向共产国际，不向斯大林屈服的，他是坚持共产党要跟国民党搞政党政治，搞合法斗争的这么一位总书记，因为这个问题最后被共产国际开除党籍，后来又被宣布为托洛茨基分子。<br />
<br />
第二位总书记就是紫阳以前的胡耀邦总书记，他是邓小平很好的一位战友，他们在相当长的时期当中，他们两个人是在一起工作的，邓小平对胡耀邦是很信任的，胡耀邦对邓小平也是很尊重的，当邓小平为了挽救共产党而进行经济改革的时候，耀邦是支持他的。也就是说改变毛泽东确定的经济体制，就是人民公社啊，计划经济啊，越大越公啊，那一套东西。<br />
<br />
不仅如此，胡耀邦还在平反冤假错案上救了共产党，我想，全世界的冤假错案，恐怕中国是一个最大的制造冤假错案的加工厂。冤假错案是数以百万计，千万计，就在这几十年当中。据我知道，耀邦在当中央组织部长跟当中央总书记期间，由他组织平反的冤假错案，大概是几百万件。<br />
<br />
当然不是他一个人平反的，但是，是在他的组织下，在他的推动下，在他的号召下，也在他亲自干预下，一共平反了几百万个冤假错案。如果几百万的冤假错案保留着不平反，我看中国共产党没有稳定可言，没有信任可言。<br />
<br />
所以，耀邦对80年代的改革是起了极大的作用。但是，邓小平对他不满意。邓小平认为，耀邦对资产阶级自由化反击不力，没有贯彻邓小平的意图，逼得耀邦无法工作，只好自己辞去总书记的职务，最后，他是在心情非常压抑的情况下心肌梗塞去世了。<br />
<br />
紫阳，我想应该跟这两位总书记当之无愧的并列。他们三个人在中国共产党的历史上确确实实是为老百姓做了工作，他们是站在弱势群体这一边，为工人，为农民，为受苦的劳苦大众做了事情。<br />
<br />
而当邓小平为了要巩固共产党统治权，或者叫领导权而镇压学生，镇压市民的时候，紫阳清醒的提出了一条跟邓小平完全相反的路线。邓小平说，这是动乱，后来说，这是暴乱。赵紫阳始终认为，学生反腐败也好，要民主也好，都是为了我们国家好，是爱国。<br />
<br />
邓小平认为，追悼胡耀邦，就是打了了邓小平的耳光，因为是邓小平亲手把胡耀邦搞下去的。(他认为)学生怀念胡耀邦不就是对我邓小平的示威吗。而赵紫阳认为，既然耀邦同志是我们的政治局委员，过去又是我们党的总书记，我们党内也在为他哀悼，那学生对他进行哀悼也是理所当然的，无可非议的。<br />
<br />
邓小平认为，解决这样的问题，首先要判断性质，这个性质就是反党，反社会主义，有计划，有预谋，有组织动乱，暴乱，应该这样来定性。紫阳认为，学生都是希望我们进一步改革，学生并不是反对我们改革，而是认为我们改革还做得不够好，应该把改革搞的更深入，由经济改革扩大到政治改革，由经济方面否定毛泽东到政治体制上否定毛泽东。<br />
<br />
因此邓小平解决问题的方案就是，拿军队来，几十万军队，一直摆在这一带，就是长安街这一带。紫阳坚持的是，对学生的要求，必须在社会各界协商对话，这样一种方式来解决，一定要争取民主和法制的轨道。这个都谈的清清楚楚。<br />
<br />
一开始的时候，我要说，一直到5月13号的时候，连邓小平还亲口当着杨尚昆的面跟赵紫阳的面说：“都同意。”他同意赵紫阳的意见，这是5月13号。<br />
<br />
赵紫阳那个时候，大家都知道，是先到了朝鲜一趟。回来以后，赵紫阳发现，根据邓小平的定性而写的那篇人民日报社论，是一篇不好的文章，是这篇文章把矛盾激化了。因此赵紫阳就针锋相对的提出了另一个解决问题的原则，在民主和法制的轨道上来解决问题。<br />
<br />
赵紫阳的这个意见以及有关他的其它反腐败的措施，发扬民主的措施，在5月8号的政治局常委会上跟5月10号的政治局会上都得到了多数的拥护。5月13号，紫阳和杨尚昆两个人跑到邓小平家里去谈如何接待戈尔巴乔夫的事情，紫阳重提，政治局常委跟政治局两次会议关于如何在民主和法制的轨道上解决学生要求解决的问题。邓小平说了三个字：“ 都同意”。<br />
<br />
四天以后，5月17号邓小平在家里召开常委会，按照党章，他没有权力召开常委会，没有权力召开常委会！他只保留一个中央军委主席，那么按照党章，中央政治局会议由总书记主持召开，在赵紫阳不知道的情况下，邓小平决定召集常委会，讨论学生问题。<br />
<br />
紫阳提出了方案说，现在要缓解，不要激化矛盾，原来人民日报把学生定性为动乱这个调子太高，无法使学生平息。紫阳建议修改这篇社论，重新定性，也就是否定第一次定性。<br />
<br />
紫阳这个意见，紫阳一票，胡启立一票，赞成这个意见，姚依林，李鹏反对。二比二。常委是五个人，还有一个乔石，乔石原先跟紫阳说，他认为那篇社论不好。在5 月15号的政治局常委会上，乔石还主张修改那篇社论，因此(当时)是三比二的票。到了5月17号在邓小平家里开会的时候，乔石保持中立：最后党怎么决定就怎么办吧。因此，就变成二比二，中间有一票(弃权)。<br />
<br />
在这种情况下，邓小平说：好，多数决定，否定你赵紫阳。其实根本没有表决，二比二。在这个情况之下，有没有办法解决这个问题呢？有办法，按照政治局常委会跟政治局会议的议事规则，如果常委会遇到重大分歧的时候，不能解决的时候，应该请求召开政治局会议。所以当时应该召开政治局会议，<br />
<br />
政治局委员，除了三个委员不在北京以外，统统都在北京。这三个人，噢，是四个。上海有个江泽民，天津有个李瑞环，四川一个，还有广东一个。这四个人的飞机半天就到齐了。<br />
<br />
说是常委的多数，按照党章，常委无权推翻政治局的决定。政治局已经在5月10号做出决定，同意在民主和法制的轨道上解决问题。你怎么能够到了5月17号，所谓常委，你就是多数，也无权推翻。更何况不是多数，是2比2。<br />
<br />
所以在这个情况之下，首先，如果按照中华人民共和国宪法来说，宪法的第二条规定，中华人民共和国的一切权力属于人民，那么，像这样的学生学潮的大事应该由人民，也就是紫阳所说的社会各界协商对话。<br />
<br />
如果按照下一句话，人民行使国家权力的机关是全国人民代表大会，那么就应该召开全国人民代表大会。万里当时也提出来，全国代表大会成立一个反腐败的机构，来满足，来解决学生所关心的反腐败问题。这都是可以的嘛。<br />
<br />
现在是，第二条没有了，变成了由中央政治局常委来决定。中央政治局常委又反过来推翻政治局的决定。政治局领导常委，而不是常委领导政治局。当时就是这么一个局面。<br />
<br />
这个局面你说是奇怪的，但是它是必然的。因为从毛泽东以来，中国共产党实行的就是独裁体制，就是列宁所说的民主集中制。也就是说，所谓的无产阶级专政，也就是共产党专政，就是领袖专政。而邓小平尽管只是中央军委主席，他不是中央委员，不是政治局委员，不是政治局常委，但是他自己说，&quot;我是第二代核心&quot;，这是他自己说的。<br />
<br />
党章上没有核心这个话，党章上从第一条到最后一条没有说中国共产党核心怎么产生，怎么选举，任期多长时间，职权是什么，没有的，是邓小平发明了一个东西。党章上只有全国代表大会，中央委员会，政治局，政治局常委，总书记，没有一个叫做核心。<br />
<br />
他自己发明的核心，他说，毛泽东是第一代核心。如果毛泽东是第一代核心，那么陈独秀到哪里去了?好像中国共产党是毛泽东建立的。所以，邓小平在讲到这些话的时候，对党的知识，对党的历史跟党的组织原则的知识等于零。<br />
<br />
那么，他作为核心来做这个决定的，就是说，中华人民共和国的一切权力属于共产党的核心。这样，宪法第二条就必须修改。所以我认为这是一个极其重大的教训。<br />
<br />
正因为如此，所以六三六五必须平反。如果六三六五不平反，那么就意味着，宪法的第二条在实质上，也就是演变成，中华人民共和国的一切权力属于中国共产党的核心一个人。他不仅可以推翻政治局的决定，可以不把人民代表大会放在眼里，把全体人民的权利都可以加以践踏，到了这么个程度。这个教训是太惨痛了，太沉重了。<br />
<br />
张楠：刚才我们一直在谈六三六五，接下来，我们希望您对中国的现状发表一下评论。中国今年要庆祝建国六十周年，您认为中国社会目前处于什么样的一个阶段，什么性质的社会，它的前景和方向是不是对头?<br />
<br />
鲍彤：我没法给中国现在的社会给一个定义。关于社会性质的问题，历来是一个争论不休的问题。按照马克思的理论，人类应该有五个社会形态: 从原始共产主义，到奴隶社会，到封建社会，到资本主义社会，到以社会主义为初级阶段、以共产主义为高级阶段的所谓共产主义社会。<br />
<br />
但是这五个阶段并没有得到公认，这是马克思的一个设想，而且其中关于这些社会，例如奴隶社会，在东方和西方有很多的争论。<br />
<br />
资本主义社会这个名称是共产党专门创造出来贬低现代文明社会的一个特殊的概念。它看到它不喜欢的东西，统统都叫它&quot;资本主义社会&quot;。<br />
<br />
比方说，他看到有人讲民主自由，共产党当面是非常愿意用民主的口号来吸引外国人，来吸引资本家，来吸引知识分子，特别是来吸引学生的，因此毛泽东写了一本&quot;新民主主义论&quot;。但是后来的时候讲到民主，讲到自由，他就加上一句 &quot;资本主义、资产阶级&quot;，把它联系起来了。<br />
<br />
邓小平在八十年代，81年，83年，85年，87年，89年，五次反资产阶级自由化。当人家只想到&quot;小平你好&quot;，在搞经济改革的时候，忘掉了，或者原谅了，或者忽视了他实际上每隔两年都要大反一次资产阶级自由化，也就是说反资本主义化，反资本主义社会。<br />
<br />
所以呢，中国社会到底是一个什么社会，说不清楚的一个社会。我想，资本主义社会本身未必是一个恰当的名字。<br />
<br />
另外一个名字倒是清楚，就是说，是文明社会还是野蛮社会。我想这个概念应该是清楚的。我认为，文明社会就应该是公民社会，就是公民有充分权利和义务的社会。这样一个社会，我认为，现在在欧洲和北美洲的许多发达国家已经为我们开创了一个先例。<br />
<br />
而我们现在的国家离这样一种状况，远的很。跟它们比较，我认为简直可以这样说，它们已经进入了文明，而我们还处在野蛮阶段。<br />
<br />
为什么？因为公民的权利是不存在的，民权是无限小的，而官权党权是无限大的。这样一个定律实际上在中国社会里面起到作用。我想，如果是研究六三六五本身教训的话，我说，最重要的就是要看到这一点。<br />
<br />
刚才我讲了邓小平的六三六五责任，用的语言相当尖锐。现在我想为邓小平解脱一下。六三六五与其说是邓小平一个人的错误，一个人罪过，不如说是一种制度，一党制本身的产物。<br />
<br />
我想要指出这一点，对邓小平本人也不要给他过高的评价，过高的抬举，他也不过是一党制的工具跟产物，他的眼界从来没有越过一党制的视野。他所关心的，他所追求的，也无非是一党制的喜怒哀乐。没有什么了不起的事情，不要把他神化，他实际上想到的，就是为了这个党如何，他没有想到人民。<br />
<br />
不要看他说，我是中国人民的儿子，我不相信自己的儿子可以调动军队来对付自己的父母，我不相信。<br />
<br />
张楠：我们知道，国际金融危机正在冲击中国，中国的经济增长在下滑，有两千多万的农民工失去了工作。那么，人们关注，在这种情况下，中国会不会发生动乱，在您看来，经济危机对中国的政治将产生什么样的影响?<br />
<br />
鲍彤：我想这是一个很现实的，很重大的问题。现在全世界都在讨论怎么救市，这个市场现在已经发生了危机，应该怎么救市。我认为救市的中心不在于救亿万富翁，也不在于有人说救先进生产力。<br />
<br />
有人提出来，我只救先进生产力，不救落后生产力。我想这种观点是不正确的。我认为，抱这种观点，采取这种立场是可以理解的，是合法的，是容许的。他说我要代表资本家，我要代表亿万富翁，我想他应该有他的自由。他要代表最先进的企业，我想有他的自由。但是，应该有人，应该有更多的人来代表弱势群体。<br />
<br />
所谓救市，它的危险在什么地方呢？就是有几千万人要失去工作，要失去收入，要失去谋生的手段。而现在首先受到打击的是谁？是农民工，是乡镇企业。<br />
<br />
我是1987年“十三大”政治报告起草小组的组长。当起草这个报告的时候，我记得这个报告里有一句话：“乡镇企业异军突起，是它支持了中国经济的发展。”我想再加一句话，“是它支持了中国的经济繁荣达二十年之久。”<br />
<br />
今天，乡镇企业处在这么一个困难情况下，我认为，国家不能见死不救，国家的眼睛不能仅仅看到亿万富翁，我想4万亿救市的资金，不应该成为拉动豆腐渣工程，拉动腐败行为的工具。应该成为挽救现在正在生死线上挣扎的那些乡镇企业，那些农民工。<br />
<br />
这是关系到中国整个社会稳定的一个极大的问题。中国现在有多少农民，大概说有九亿。这个九亿农民大概说，是在两亿到三亿个家庭里面。九亿农民啊！大概在两亿到三亿的家庭里面。<br />
<br />
如果是农民工失业达到两千五百万人，那么就是说，十个农村家庭里面有一个人要断绝收入。农村里面，两亿 五千万个农户当中有两千五百万个人失业，意味着什么呢？十家人家里面有一家人失业。<br />
<br />
我想现在农村的收入的构成情况是非常清楚的，一个农民(家庭)如果没有一个人在城市里工作，不管是在北京，上海这些大城市或者是在省会或者是在县里的小城市工作，他寄回去的那笔钱对维持他家里面的生存是最重要的来源。<br />
<br />
如果十分之一的农民家庭断绝了这样一种谋生之道，我说，这就是变成了巨大的社会危机，不是经济危机的问题，是变成一个社会危机。社会危机他到底是在政治方面凸显出来，还是在经济方面凸显出来，还是在社会秩序方面凸显出来，没有一个人能预料到，没有一个人能预料！<br />
<br />
所以在这个情况下，我认为，经济方面采取的措施，应该是替这些弱势群体，使这些在由于经济危机的冲击而很难像过去，像前几年那样，继续用这种方式来生活的人。我说，应该重点放在这个地方。<br />
<br />
同时，在整个社会秩序方面和政治秩序方面应该着重的是什么呢？应该着重关怀，体贴，缓解这一部分矛盾。如果说，把这一部分问题放在自己视野之外，那么我看，起的作用又会重复邓小平的错误，就是说，不断的激化矛盾。如果犯这样的错误，那么我想，在经济危机如此深重的情况下，在全球范围里面出现(危机)的情况下，那就叫雪上加霜。<br />
<br />
张楠：您是那一年加入中国共产党的？<br />
<br />
鲍彤：1949年。<br />
<br />
张楠：您当时为什么要加入共产党？<br />
<br />
鲍彤：为了民主。我当时是中学生，当时在上海念的中学叫南洋中学。为什么叫南洋中学呢？因为当时的交通大学本来叫南洋工学，我们的校长叫王陪荪，他是南洋中学也就是交通大学第一期的学生。毕业以后呢，他就把自己家里的私塾，王家的一个私塾变成了南洋中学。<br />
<br />
我在南洋中学读书加入共产党时，人家问我，你的介绍人是谁？我说，有两个介绍人，一个介绍人已经死了，两次冤假错案，最后跳井死了。他是我的同学，比我低一班的同学，这个人我今天不多说了。还有一个介绍人是蒋介石，蒋介石用他的腐败，用他的独裁，介绍我加入共产党，<br />
<br />
所以当我入党宣誓的时候，我的入党介绍人，也就是后来跳井自杀的那个，他两次遭受冤假错案，最后在文化大革命当中自杀了。他跟我说，我们要找一个党，一个最民主的党，就是共产党。我说，好，最民主的党是共产党，我们就加入它。这是1949年3月的时候，上海5月就解放了。<br />
<br />
张楠：那么作为一个曾经的老党员，我们知道不久以前，您又签署了08宪章，这又是为什么？<br />
<br />
鲍彤：一回事。我想08宪章有人说是非法的，我看来看去，它就是一个普法教材，是宣传中华人民共和国的宪法的，是宣传中华人民共和国宪法最重要的一个条文。<br />
<br />
宪法第二条，中华人民共和国的一切权力属于人民，也是宣传中华人民共和国的最重要的内容；第二章，中国公民的基本权利和义务。<br />
<br />
就我所说，08宪章不是什么非法的东西，它应该是我们中华人民共和国的普法教材。就像美国有一本书，当年17世纪的公民佩恩(PENN)写的那个“常识”。如果说“常识”应该普及，我想，法律知识更应该普及，所以我说，08宪章无非是常识的普及，中国宪法的普及，正因为我为了找民主而找了共产党，因此，我今天为了找民主，我必须在这份普法教材上签字。<br />
<br />
张楠：您签了字，有没有遇到麻烦呢？<br />
<br />
鲍彤：到现在为止没有遇到什么大麻烦。<br />
<br />
张楠：当局也没有跟你谈什么？<br />
<br />
鲍彤：问过我，为什么去签署？我说，因为我同意这些内容，所以我签署。我说这次我签署，我是根据宪法第二章，公民的权利和义务而作的事情。我想这个是一样的，就好像我今天接受采访，这也是我作为公民的权利和义务。<br />
<br />
因为国务院有个规定，向全世界宣布，说国际媒体从2007年1月1号起可以自由的采访中国公民。我想，既然国务院有这么一片苦心，也向全世界表示了他的开放，那么我想，我也有这个权利和义务来履行国务院的规定。<br />
<br />
张楠：在采访一开始的时候，您谈到您现在出去还会有人跟踪您，监视您。您说为什么政府现在这么怕您？作为一个国家机器那么强大，您只是一个个人，如果说有跟您有相同政治改革理念的人，那么在13亿人口中也应该是一个少数，您说为什么政府会怕你和其他像你具有同样理念的人呢？<br />
<br />
鲍彤：我想政府没有怕我，政府是对我不放心，我估计是这么个意思。实际上我想政府这么些年应该知道一点，就是我的一切言论和行为都是合法的，都是和平的。没有暴力，没有非法的。我想这些年他们应该了解这一点。<br />
<br />
张楠：在六三六五以后，您被监禁7年，在监狱里，您都考虑什么问题？您有没有想过，改变一下自己的想法？放弃自己的这个信念？这样今后的日子会过得好一点？<br />
<br />
鲍彤：我觉得在监狱里我最大的失望就是，我作为主管政治体制改革的人，没有把自己的事情做好。我想，所有一切问题都是由于我们现在在政治体制方面仍然延续了毛泽东时候的那套东西，没有根本性的触动，因此产生了一系列的问题。<br />
<br />
这些事情我自己感到内疚，我自己感到没有胜任自己所应该做的工作，因此在我坐牢以后，在反复思考以后，我有责任把自己这一段时间反复考虑的一些问题告诉大家，和大家一起讨论。<br />
<br />
张楠：哪些问题？<br />
<br />
鲍彤：中国的人权，中国的民主，中国的自由，中国的共和制度。我想这些问题是最重要的。<br />
<br />
张楠：现在你那些过去的老朋友，或者在党内的一些老同事，还能跟他们自由的见面和交谈吗？<br />
<br />
鲍彤：我不知道，我只能说，我从1989年5月26号离开紫阳，28号到秦城后，我一直没见过谁，直到紫阳死我也没见到。<br />
<br />
紫阳是2005年1月17号去世的，上午的时候，紫阳的女儿给我的女儿发了短信：“他自由了。”就这4个字，一看我就清楚了，“他自由了”，就是他去世了。<br />
<br />
当天我没有想去，因为我想他家一定忙的不得了，没有时间。当天下午我就到一个布店买了黑布，黑纱，第二天我跟我太太，我说我们两个人去看看紫阳夫人，看看孩子。大概早上9点钟，一出去到那个电梯的地方，那里全都站满了人。见了我，他们说：今天你不能出去。这就是我得到的信息，紫阳就是去世了我也不能见他。我说：你们这样是不合法的，没有道理的，是没有人情的。<br />
<br />
张楠：像以前这些老同事，老上级，你现在也见不了。如果见到他们的话，你最想跟他们说什么？<br />
<br />
鲍彤：我最想说的是：咱们用什么办法来救救老百姓？如果他们最关心的是党，那么我也想请他们考虑，咱们用什么办法来救救这个党？我认为真正要救这个党，只有一条办法，劝他放弃一党专政。我认为丢掉一党制，共产党就可以得到自己新的生命。<br />
<br />
张楠：我们知道，在胡锦涛，温家宝政府任内，中国发生了许多大事，比如：非典事件，西藏骚乱，汶川地震，奥运会，还有金融危机的冲击等，你对现在政府的表现作何评价？<br />
<br />
鲍彤：我认为它作了很多事情。我认为，去掉了一党专政，他们会做得更好。<br />
<br />
张楠：那么您目前日常生活中都作些什么事情？<br />
<br />
鲍彤：那就是生活了。上医院看个病啊，到书店买本书啊。近两年来，2007年以来，我还有一项任务，就是跟记者们会面，这是我很大的乐趣。要不然的话，我相信我的嗓子，我的声带要萎缩。<br />
<br />
张楠：大家过春节，中国人都讲究串门啊，拜年啊，您能不能自由的串门，拜年？<br />
<br />
鲍彤：我实际上没有到谁的家里去，一家也没去，因为中国有一个根深蒂固的传统，就是株连，就是一个人如果说是被最高领导人，共产党宣布为人民公敌，他就好比是传染病一样，走到哪里，就成为哪里不祥的一个兆头，所以我没有去看任何人。<br />
<br />
张楠：您是那年生人？<br />
<br />
鲍彤：1932年11月5号。所以现在我76岁已经过了，到今年的11月，我就是77岁了。<br />
<br />
张楠：身体怎么样？<br />
<br />
鲍彤：身体比上不足，比下有余。 我现在的医疗条件应该说比中国绝大多数人都好。<br />
<br />
张楠：你享受什么待遇？<br />
<br />
鲍彤：我享受的是社会救济的待遇，但是我的医疗费可以是100%的报销。<br />
<br />
张楠：那是按离退休干部待遇？<br />
<br />
鲍彤： 不是。我知道中共中央发了一个红头文件给北京市委，说鲍彤以后的生活问题由你们北京市处理，每月发给他500元生活救济费，至于医药费，可以实报实销，这是1997年的一个红头文件，这个红头文件这样规定以后，当然就这么执行了。<br />
<br />
执行了10年以后，他们跟我说：你是不是生活有点困难？我说：没有什么困难，现在反正我有老伴啊，我儿子，女儿养着我呢。尽管500块钱是不够用的，但是他们都养着我呢，没有什么困难。他们说：如果有困难你可以打个报告。我说：用不着打报告。<br />
<br />
过了一段时间他们主动说：领导上决定给你加生活救济费，从500块加到2000块。我讲笑话说，我叫翻两翻，提前全面建成小康，达到小康水平，500变成2000。<br />
<br />
何宗安：我只想问你：你说你不能去别人的家，如果我请你到我家去吃饭，可以吗？<br />
<br />
鲍彤：如果你不怕株连，我一定欣然接受邀请。<br />
<br />
张楠：好，谢谢您。很难得跟您谈，其实还有问题，我都跳过去了，因为时间太长了。<br />
<br />
何宗安：你可不可以再说说宪法里面的......?<br />
<br />
鲍彤：这是中华人民共和国的宪法，我想这就是我们国家的根本法。按照这个宪法，中华人民共和国的一切权力属于人民。我认为这部宪法的全部价值就在这句话上。有了这句话，这部宪法就成立了，就有生命了；没有这句话，这部宪法就毫无意义。<br />
<br />
所以，如果说，按照中华人民共和国的一切权力属于人民，那么我认为，学生示威游行是合法的；学生追悼胡耀邦去世是合情合理的；学生要求反腐败，要求接受民主制度，显然是合情合理的爱国行为。如果是根据这一条，那么我认为，中国共产党的核心，无权决定动用军队来镇压人民。<br />
<br />
实际上当时已经牵涉到这样一个问题，是万里亲自提出来的，他是全国人民代表大会常务委员会的委员长。他说，他打算向全国人民代表大会常务委员会建议，成立一个反腐败委员会，专门对官员进行监督，专门受理高级官员及其家属的腐败问题，这也符合规定的。<br />
<br />
你看，这里的第二条规定：人民行使国家权力的机关，是全国人民代表大会和地方各级人民代表大会。如果要落实这一条，我看也好了。<br />
<br />
所以，实际上，如果按照赵紫阳所说的，在民主和法制的轨道上来解决问题，就是符合我们整个宪法的精神，那就可以使我们的国家受到一次洗礼，洗礼呀! 一次民主的洗礼，法制的洗礼。从此把人们的权力真正成为国家的主体。<br />
<br />
现在我们的国家实际上是一个官权，党权压倒人权的这样一个社会，这是一个很不正常的现象。就这一点上来说，我们的国家现在还没有资格叫共和国，因为世界上真正的共和国不是这样的。<br />
<br />
张楠：如果当时真是按照赵紫阳，万里他们的设想来走的话，那么国家是不是和现在有很大的不同？<br />
<br />
鲍彤：我相信，有可能。比现在台湾的民主制度搞的更好一些，我有这个信心。当时实际上，在六三六五的时候，你问我很多六.四的情况我不清楚，因为赵紫阳给我的任务就是通过这次学潮，我们学会协商对话，怎么样学会在社会各界建立民主制度， 受一次法制的教育。<br />
<br />
如果拿这个东西作为一个起点，那么十三大通过的政治体制改革的那个方案，就可以进入我们社会的实际生活；我相信中国人并不笨，既使是在抗日战争当中，即使在文盲当中，我们当时，据我知道，华北的有些地方是民选村长，民选乡长，还有民选县长。<br />
<br />
可是，现在很遗憾，连中央委员都不能选举自己的总书记，只能等额选举；连人民代表大会的代表也一样，你看他们的学历啊，都是博士生导师啊，硕士啊，但是到选国家主席的时候，都还是等额选举，一个候选人选一个当选人。既没有竞争也没有选择。不竞争，不选，这个叫什么共和国？<br />
<br />
今年是中华人民共和国60周年，一个人从生出经过60年那就变成老人了，一个党经过60年应该说成熟啦！这个60年的国家你总不能说他是小孩子了，但是我们连竞选都不知道是什么东西，我想这是一个歧路，这说明我们离文明社会的门坎还很远。<br />
<br />
但是，并不困难，要选举，我看也可以。请问，美国独立战争以后第一次选举用了几十年的时间选举，才决定选举的华盛顿，有没有这么回事？做就能做起来，不做，永远做不起来。<br />
<br />
现在做不做，不是条件具备不具备的问题，而是中国共产党中央委员会有没有承受这个能力？它敢不敢让人民，让中国的公民来选择自己的国家元首？ 它如果没有这个信心，那么，中国不可能成为一个共和国；它有这个信心，我想应该可以。<br />
<br />
毛泽东在一九三几年的时候逼蒋介石的时候说，中国现在就可以进行竞选，普选，直选。毛泽东对蒋介石提出的要求，在当时是相当干脆的。 那么我想，中国共产党也应该对自己要求得严格一点，就把自己当成蒋介石，那么你也应该选举。<br />
<br />
何宗安：所以你比较乐观，是不是？<br />
<br />
鲍彤：我很乐观，我相信这没有什么克服不了的困难。相反，如果把这些问题都想通了，那就叫把包袱放下了。毛泽东有一个很形象的话，把一些错误的观念放在脑子里面，脑子会压得昏昏沉沉，就好像背上背了一个大包袱，走路也走不快，头脑也糊里糊涂，不清爽；放下包袱，马上就轻装上阵。<br />
<br />
我想，这个就是中华人民共和国的真正的前途；中国公民的真正的权利也就取决于这里。当然，像这样的事情必须由中国公民自己去争取，天上掉不下来；不争取人家永远不会给你，要靠别人来帮忙，我看是很难的。还是那句话说得好，“自力更生”，自己解决自己的问题，自己应该选择自己认为正确的制度。<br />
<br />
我看，没有普遍真理，但是有普世轨道，普世价值。学世界大家都在走的路，他们已经走了200年，300年，400年，像美国，像法国，像英国。他们所走过的路，我认为是成功的。<br />
<br />
至于我们自己想的那些东西，尽管也是从德国，从大英博物馆马克思的书里面来的，但是到现在为止，没有一个国家成功过，中国现在只保留了中国共产党的名字叫“共产党”，实际上，它纲领上没有共产，我想，真正的共产主义是从人道主义来的，很可惜，现在连人道主义都没有。<br />
