													<h2>西安14岁女孩因长期营养不良早逝</h2>
						
						
																											<div id="postmessage_847765" class="t_msgfont">西安14岁女孩因长期营养不良早逝(组图)<br />
 来源：西部网 2010年09月28日<br />
&nbsp;&nbsp;小[提要] 9月26日，西安14岁少女小灵(化名)去世，原因让人唏嘘：长期的营养不良导致贫血等多器官衰竭。什么让小灵遭此困境？2006年父亲下岗，每天捡破烂为生，母亲2007年因病去世，全家每月收入不到三百，几乎顿顿都是馒头面条。小灵今年刚升初三，身体不舒服也没钱去医院检查，只是在家休息，直到腿软撑不住了，但已回天乏力，年轻的生命就此终结。[网友哀叹] <br />
 　　本报讯 （记者 李小博） 看着女儿的遗像和骨灰盒，赵利营再也坐不住了，他打开衣柜整理出女儿的衣物，然后抱着女儿遗像痛哭起来。<br />
<br />
　　妻子去世三年后再失独女<br />
<br />
　　昨日下午3时许，在西安市东郊金花坊小区10号楼前，48岁的赵利营怀抱着一堆各色衣服缓缓地走到楼门口的垃圾台跟前，将衣物丢进去。他14岁的女儿小灵（化名）26日上午刚刚去世。<br />
<br />
　　走进赵利营一楼的家里，昏暗的屋子里散发着一股霉味。赵利营的妻子2007年因病去世，全家就剩下他和女儿相依为命，“今年她刚升初三，8月30日还去学校了一趟，但再也没有去上课。”赵利营说，自开学后，女儿就觉得身体不舒服，“家里没有钱，没有给孩子去医院检查，就让她在家里休息。”赵利营说，他发现女儿吃东西很少，而且一吃就吐，“到了24号上午，孩子突然说腿软，我就赶紧送她去医院。”<br />
<br />
　　“很少在家吃饭，都是在外面吃”<br />
<br />
　　赵利营说：“都说虎毒不食子，这是我的孩子呀，没有钱我咋办？”床边柜子上放着孩子的骨灰盒，他从背后拿出女儿的遗像，照片里的女孩穿着校服，静静地盯着前方。“孩子和她妈的户口在她外婆家，过两天我要把她们的骨灰埋在一起。”<br />
<br />
　　“她很少在家吃饭，都是拿钱在外面吃，也很少和我说话，就是哪里有个不舒服也不告诉我，我知道孩子营养不良，但就只有这个能力。”<br />
<br />
　　“孩子平时最多在家里吃个早点，都是我买好的油条、豆浆，放学回家我就是做一些面条或者米饭。”赵利营说，女儿很喜欢吃麻辣味的东西，经常会买一些麻辣串，“我有时候问她吃饭没有，她就说吃了肉夹馍、水煎包，女孩子嘛，就是爱吃零食，很少整顿地吃饭。”说完，赵利营又哭了。<br />
<br />
　　长期营养不良为死亡原因<br />
<br />
　　“以前她妈也没有工作，但1000来块钱还能过活，自从我2006年下岗后，她妈也生病，这个家就乱了。”赵利营说，自妻子去世后，女儿就变得有些沉默了，而且很少和他交流。<br />
<br />
　　赵利营说，他每天就是靠捡破烂为生，“一个月的收入只有二百七八，根本没法养活孩子。”<br />
<br />
　　提起小灵的去世，小区75岁的杨阿姨不住地叹息，“多乖的娃呀，每天上下学都从这条路上经过，可惜这一下子不在了。”杨阿姨说，小灵的去世主要是因为营养不良，“总见到他（小灵父亲）提着馒头和面条回家，很少看到他买菜，孩子想吃上一顿肉都很难。”而另一位住户说，小灵的父亲以前有工作，“但下岗后就再也没有上过班，爱喝酒抽烟，是个可怜人。”<br />
<br />
　　昨日下午5时，得知小灵已经去世的消息，班主任王老师有些惊讶。“我们24号晚上还去医院看她了，看着精神状态还不错，怎么一下子就没了。”王老师说，小灵的成绩属于中游水平，“她不爱说话，但是和同学们的关系都挺好的。”<br />
<br />
　　从西京医院的死亡通知单上可以看到：小灵24日上午11时22分来医院就诊，26日上午11时05分去世，诊断一栏写着：感染性休克、恶液质，死亡原因为感染性休克。昨日下午6时，西京医院急诊科大夫称，小灵的病因为长期的营养不良导致贫血等多器官衰竭死亡。<br />
<br />
　　大夫解释称，恶液质是指由于全身许多脏器发生障碍导致的一种中毒状态。<br />
<br />
　　记者就此咨询了西安一家三级甲等医院保健医生，她说营养不良常有两种症状，消瘦：由于热能严重不足引起，身材矮小、消瘦，皮下脂肪消失；另一种为浮肿。表现在体格方面，则是体重低、生长迟缓、消瘦，而且会并发低血糖、低体温、贫血等。<br />
<br />
　　要避免营养不良，主要有三个方面。饮食平衡：通过饮食的合理搭配实现营养摄取平衡。营养补充：可以通过注射等手段。营养强化：持续地加强某种营养的补充，如服用药物等。而一些可能引起营养不良的疾病，如神经性厌食症、偏食等，则需要及时治疗。 来源：华商报</div>

							
							
							
							
