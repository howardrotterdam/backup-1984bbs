													<h2>14岁女孩早逝市民痛心 营养不良诱发多病症须警惕</h2>
						
						
																											<div id="postmessage_848931" class="t_msgfont">华商报讯(记者李小博) 因为长期营养不良，14岁女孩小灵(化名)早逝，昨日本报报道后，市民来电纷纷表达对小灵的惋惜之情，也对她的精神状态和生活习惯提出看法。<br />
<br />
　　家人：爱吃油炸馍蛋奶常送人<br />
<br />
　　昨日，小灵的父亲赵利营仍没有从失去爱女的痛苦中缓解过来。“我平常的确对她的关心不够，比如不回家吃饭时，她告诉我，吃的都是些油炸馍、炸麻辣串什么的，喝的就是饮料，现在家里还有她买的麻辣片呢。”赵利营说，他觉得女孩子爱吃零食，自己也没有太在意，“我还会在家里炒菜给她吃的，每天给她三五块钱，但是在外面她吃不吃菜，我真的没有过问过。”赵利营说，他欠下女儿的太多了。<br />
<br />
　　小灵的好友小梦(化名)说：“学校里有‘蛋奶工程’，我只见过她吃了一两次，我问她为什么不吃，她说不喜欢。”小梦说：“平常在一起有说有笑的，也不知道她的身体不好。”<br />
<br />
　　赵利营也认同了小梦的说法，“我见她好多次都把牛奶鸡蛋带回家，我就问她为什么不吃，她说不爱吃，有时候害怕我说她，就悄悄把东西送给同学了。”<br />
　　专家：膨化油炸食品没有营养<br />
<br />
