<br />
　　段玛瑙说，营养不良是因为营养素的缺少或过多及其代谢障碍造成的机体营养失调，“原发性营养缺乏病是由于膳食中的营养素不足造成的，而因为机体对营养素的摄取消化吸收和利用障碍则属于继发性营养缺乏病，因为体内营养素摄入不足和吸收不良引发的。”“一般来说，营养不良会直接导致贫血，导致胃蠕动缓慢，破坏消化系统，随着贫血加重，会并发贫血、钙缺乏症、锌缺乏症、佝偻病等。”段玛瑙说，营养不良往往表现为身材瘦弱矮小、头发枯黄干燥易脱落、体弱乏力、精神萎靡不振。“比如像膨化油炸食品，不会对身体补充任何的营养。还有碳酸饮料，会导致体内的营养成分和微量元素流失。”“像小灵的情况，我认为她主要是由于精神忧虑导致的厌食，还有就是生活无规律。”段玛瑙说。<br />
