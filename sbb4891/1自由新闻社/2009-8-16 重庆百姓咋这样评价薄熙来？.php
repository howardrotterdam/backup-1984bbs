四中全会前！薄熙来大打出手 终引太子党众怒 <br />
&nbsp;&nbsp;<br />
姜平 <br />
&nbsp;&nbsp;<br />
【人民报消息】中共定于9月份召开党的十七大四中全会，主要是安排下一届的人选。从目前的安排和年龄来看，政治局委员、重庆市委书记薄熙来下届进政治局常委会是决没有希望，这让野心日日上涨的薄急到头昏脑胀，东出一拳、西踹一脚，终于引发元老和太子党们众怒。<br />
