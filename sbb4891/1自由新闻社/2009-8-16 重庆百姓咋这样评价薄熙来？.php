重庆百姓咋这样评价薄熙来？<br />
<br />
两江居士<br />
<br />
<br />
因为休年假，我于7月25日去了重庆，8月4日离开，一直住在位于龙湖西苑的亲戚家中。<br />
8月3日傍晚，我和家人在龙湖西苑的一处公园中散步。当行走到一处有桌子、有椅子的地方时，见到几位老人围坐在那里聊天。当我听到有薄书记的字眼时，便有意识地停住了脚步，仔细地倾听他们的谈话。<br />
一位带着眼镜的老人，缓缓地从椅子上站起来了，他说：“薄书记提出建设‘宜居重庆’、‘畅通重庆’、‘森林重庆’、‘平安重庆’和‘健康重庆’的理念，不仅符合重庆的长远发展需要，也特符合我们的心愿。大家说是不是啊？”<br />
“对头！”<br />
“大家还别说，我最欣赏薄书记‘健康重庆’的提法。我认为，这个‘健康重庆’所蕴藏的内涵，不仅仅局限于我们老百姓的身体健康，而且还包含着怎么样正确评价毛主席的特殊含义。如果象某些人那样恶意地诽谤毛主席，恶意地妖魔化毛主席，能说这个社会是健康的吗？所以，薄书记要建设‘健康重庆’，特能提人精气神，我举双手拥护。”另一位老人激昂地发表了自己的看法。<br />
一位老人接着他的话题说：“薄书记是妖魔化毛主席的死对头！”<br />
“其实，‘健康重庆’的含义还包括建设‘廉洁重庆’的内容，大家不看看，自薄书记来到我们重庆后，有多少贪官倒在薄书记反腐的利剑之下？他入主重庆的第一把火，就有7名高官被双规，从而引爆官场大地震，这或许就是建设‘健康重庆’的前奏。我以为，只有官场健康了，重庆才能真正地健康起来。大家认为我说得对头不？”这位看来好象有过官场阅历的老人用目光征询着大家的意见。<br />
“是这个理，薄书记生来就是贪官的克星！”几位老人一致附和。<br />
“我给你们讲一桩我亲眼看到的事情。前几天，我应邀去一家豪华大酒店吃饭。席间，一些官员模样的人，与我同桌。一问，才知他们是某单位的几位处长、局长什么的。正当他们杯觥交错之际，门外仿佛有人提到薄书记的名字，只见那几位官员举着酒杯的手，好象凝滞在空中，许久都没有放下来，你们说好笑不好笑？”他一边用手中的拐杖，重重地敲击地面；一边敞开胸襟，发出呵呵的笑声。<br />
“薄书记原来还是打鬼的钟馗啊！”几位老人也相继哈哈大笑了起来。<br />
一位看起来背有点驼的老人接着说： “我从报上看到，说薄书记在来重庆时，和他的妻子商量好，决不允许任何亲友以及身边的任何人在重庆图方便、求特权。他是这样说的：‘如果大家听到有谁打着我们的旗号在重庆办事情，请一定坚决制止并告诉我们’。薄书记是扑面的春风，我们重庆确实太需要铁腕整顿吏治了，也确实太需要象薄书记这样的带头人了！”<br />
这时，一位身穿背心的老人则表达了自己独特的见解。他说：“薄书记是一只专门吃黑的老虎，他把那些涉黑的亿万富翁一下子就给掀翻了，大快人心啊！这等于给我们吃了一颗定心丸，让我们有了安全感，有了舒畅感，有了幸福感。这可是薄书记献给国庆60周年的厚礼啊！”<br />
他的话音刚落，掌声顿时响起，我也禁不住鼓起了手掌。<br />
正当我全神贯注地倾听着他们的谈话时，一个清澈的童音从不远处传来：“爷爷，我们回家喽！”<br />
只见那位拄着拐杖的老人亲切地应了一声后，对大家说：“我们有的是时间聊，明天再接着这个话题，如何？”<br />
