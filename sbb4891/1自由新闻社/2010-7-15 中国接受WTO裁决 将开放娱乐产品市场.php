																											<div id="postmessage_743018" class="t_msgfont">中国接受WTO裁决 明年开放娱乐产品市场<br />
<br />
中美双方昨日表示，中国同意遵循世界贸易组织WTO的裁决，于2011年3月19日以前对美进一步开放娱乐产品市场。美国曾诉讼中国对进口和分销书籍、音乐、电影和其他娱乐产品设置障碍。<br />
<br />
承诺履行WTO裁决意味着中国必须对美国电影、音像制品和图书产品的进口政策作出调整。在WTO的判决中，允许中国保留两家国有电影发行公司的进口权，并保护了中国政府对外国电影进行一定程度审查的权利，中方也无须提高每年引进20部外国电影的限额。但是，中国将不得不允许美国及其它外国企业向中国引进电影、音乐、电游和图书，并准许美中合资企业在互联网上分销音乐。<br />
<br />
据悉，从2003年起，国产片的票房已经连续6年反超进口片。市场人士认为，相比较之下，音像制品遭受的冲击可能更为直接。记者昨天就此采访了太合麦田的负责人宋柯，他表示未来政策改变不会对中国原创唱片产业造成很大影响，他同时认为此事对演出市场应该有些促进，“我们之前也可以做国际艺人的演唱会，但相对较少。如果将来市场打开，演出的引进方多了，量大了成本就会下降，就会促成良性市场竞争。”<br />
