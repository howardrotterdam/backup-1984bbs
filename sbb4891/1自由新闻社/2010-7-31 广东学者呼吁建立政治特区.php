&nbsp; &nbsp; <br />
&nbsp; &nbsp; 专家认为，尽管广东人明知胡锦涛集团并不想进行政治体制改革，但仍在坚决提出建立政治体制改革特区，这意味着广东开始向胡锦涛逼宫了。<br />
