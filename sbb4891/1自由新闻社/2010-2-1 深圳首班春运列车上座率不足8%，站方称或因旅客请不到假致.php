																											<div id="postmessage_460767" class="t_msgfont">“旅客请不到假致上座率不足8%” 深圳火车站发出的首趟春运列<br />
<br />
<br />
<br />
<br />
2010年02月01日03:40&nbsp;&nbsp;南方都市报<br />
<br />
<br />
<br />
<br />
本报讯 (记者 刘凡)春运第一天，深圳火车站发出的首趟可搭乘1000多名乘客的L 252次列车只有100多名乘客乘车，上座率只有8%，昨日，铁路部门解释，是9成旅客错过了乘车，原因可能是因为请不了假或者其他不明原因。<br />
<br />
　　几乎空车！<br />
<br />
　　1月30日是春运的第一天，零点33分，实行实名制以来的深圳首趟春运列车―――开往汉口的L 252列车―――由深圳火车站开出。令人惊讶的是，这趟拥有18节车厢，原本可容纳972个座位和540个卧铺、至少可搭乘1512名旅客的列车空空荡荡，乘客加起来装坐满2节车厢，只有不足120人，上座率不足8%。<br />
<br />
　　记者当天在深圳火车站看到，火车站冷冷清清，查验证件窗口也没有一个人在排队验票。<br />
<br />
　　信息有误？<br />
<br />
　　然而，根据铁路部门发布的信息，这趟列车的车票早已售罄。记者了解，早在1月24日，广州铁路集团公司就通过媒体发布信息称，截止到2月1日之前的所有火车票基本售完。1月26日再次发布信息称，旅客可以买到少量前往武昌、长沙等方向的车票。<br />
<br />
　　是否车票信息发布有误？广州铁路集团公司何志文称，这个票务信息是准确属实的。既然车票已售罄，900多名乘客既不乘车，也不退票，又如何解释？何志文表示，他不清楚这趟列车上座率不足8%的情况。<br />
<br />
　　深圳火车站党办副主任易非凡表示，可能是时段不集中，900多名旅客错过了乘车，有旅客反映请不到假等其他不明原因。<br />
<br />
　　难道是900多名旅客集体请不到假，错过了乘车？铁路部门的这一说法遭到了网友的炮轰。昨日，有网友称，其实每年春运都有这样的怪现象，一面是一票难求，一面却是车厢空空如也，中间的利益有多深还真是难以琢磨。也有网友调侃称，“请不到假赶不上车”，预计将成为2010年又一流行语。</div>

							
							
							
							
