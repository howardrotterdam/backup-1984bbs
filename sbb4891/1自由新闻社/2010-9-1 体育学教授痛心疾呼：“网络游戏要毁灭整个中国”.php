近日，江西财经大学副校长易剑东教授在个人博客中发表文章，对当今网络游戏在青年人中盛行表示担忧。文章的标题赫然打出：“网络游戏要毁灭整个中国！”文中称，欧美虽然有游戏产业和网络游戏群体存在，但支撑社会的主要活动方式，尤其是学生群体的主要业余生活，并没有被游戏所控制。如果放任大量不健康的网络游戏，侵蚀我们青少年和儿童的肌体乃至道德和心理，将有亡国的危险。<br />
<br />
<br />
　　易教授在国内体育界享有一定的权威性，他的观点得到了大多数网友的认同，但也有网友认为，游戏本身没坏处，就好比刀用好了能切菜，用不好就是杀人。政府相关部门以及社会各界，应该多提供些体育锻炼的场所，保证青年人有地方锻炼身体。<br />
<br />
<br />
　　网游让孩子们难以自拔<br />
<br />
<br />
　　文中指出，现在的“90后”、21世纪出生的孩子是网络游戏的主力军。大学生、中学生、小学生甚至幼儿园孩子已成了网络游戏的主要消费群体。这些游戏最大的弊病有以下几点：<br />
<br />
<br />
　　一是内容大多不健康，以杀人等作为主要方式的游戏极多，对青少年道德伦理的底线形成巨大的破坏。<br />
<br />
<br />
　　二是往往极具成瘾性，使游戏者难以自拔，使得游戏者丧失现实生活的基本规律。多数网络游戏设计了重重关卡，几乎无极限地吸引游戏者通关升级，一步步把游戏者引入所谓的奇妙境地。<br />
<br />
<br />
　　三是形成巨大的所谓游戏者群体及其话语方式，对现实社会的运行规则带来强大挑战。<br />
<br />
<br />
　　欧美青少年更爱运动<br />
<br />
<br />
　　反观欧美等地，虽然也有游戏产业和游戏群体存在，但支撑社会的主要活动方式，尤其是学生群体的主要业余生活并没有被游戏所控制。喜好户外运动、经常锻炼身体、参与体育竞争、观赏竞技表演等依然是多数青少年的主要业余生活。<br />
<br />
<br />
　　美国很多体育运动是季节式的开展。平均每年参与各项游泳、娱乐性举重、自行车等项目至少一次的人次都在1千万以上。美国泰勒研究集团发表研究报告说，仅2008年美国的网球参与人口就创纪录地达到2700万人。<br />
<br />
<br />
　　据澳大利亚政府的一项调查显示，澳大利亚15岁及以上的人口中有近1300万人参加了某种以娱乐或体育为目的而开展的锻炼活动；近1100万澳大利亚人每周至少锻炼一次，大约420万人每周至少锻炼五次。澳大利亚15岁及以上人口最喜爱的运动项目及其参与人数如下：跑步（590万人）、有氧运动或健美 （250万人）、游泳（230万人）、自行车（150万人）、网球（140万人）、高尔夫球（130万人）、跑步（120万人）、丛林徒步行走 （902500人），足球（669300人）、无挡板篮球（614000人）。<br />
