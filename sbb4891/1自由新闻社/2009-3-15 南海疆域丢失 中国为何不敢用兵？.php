													<h2>南海疆域丢失 中国为何不敢用兵？</h2>
						
						
																											<div id="postmessage_53581" class="t_msgfont">文章来源：<a href="http://blog.ifeng.com/article/2377892.html" target="_blank">http://blog.ifeng.com/article/2377892.html</a><br />
3月10日，菲律宾总统阿罗约不顾中国的反对，正式签署“领海基线法”，将中国的南沙部分岛礁和黄岩岛划入菲领土。“领海基线法”的签署，标志着菲律宾侵占南中国海海域由实质占领到法理占领。<br />
南海诸岛是我国固有疆域。东汉杨孚《异物志》对南海诸岛就有“涨海崎头，水浅而多磁石”的记载。清朝政府将南沙群岛标绘在权威性地图上，对南沙群岛行使行政管辖。20世纪60年代中后期，随着南海油气资源的被发现，南海诸岛开始被周边国家侵夺。<br />
1970年和1971年，菲律宾派兵占领3个南沙岛屿，到1975年增至6个，现在已达8个。越南目前已经占领29个南沙岛屿。马来西亚则于1983年和1986年占领了弹丸礁、南海礁和星仔礁，至今总共占有5个岛礁。文莱、印度尼西亚也宣布8万多平方公里的中国传统海疆为其“主权”所有。为捍卫主权，中国与越南、菲律宾等国在上世纪70、80年代，发生过军事冲突。<br />
1989年以后，因国内政局维稳的需要，中国政府于1990年针对南中国海提出“搁置争议，共同开发”的政策，希望以此来维护中国在南海的权益。但是，南海周边国家认为中国这一政策，恰恰暴露了中国国内的困局和政治上的软肋，在中国“搁置争议”下，越南、菲律宾等国就放开手脚“自己开发”，逐步把中国从南海的利益链条上踢了出来。<br />
如今，无论中国政府的抗议多么强烈，民众的主权意识有多高涨，南中国海被侵占和丢失的客观事实已经摆在了中国政治家和国人面前。期望南海周边国家良心发现，将已占领的海域归还中国，无异于痴人说梦。<br />
笔者在此不想去讨伐那些侵占南海疆域的国家，因为口诛笔伐没有实力的保证和支持，是毫无任何作用和意义的，与政治家们的抗议如出一辙。笔者在此从中国自身角度来剖析，中国为什么眼睁睁看着南海丢失而无动于衷，是什么原因导致如今中国在南海的被动局面。<br />
南海疆域的丧失，有其外部因素，但这决不是主要的原因，关键问题还是出在中国自身。内因才是南海疆域丧失根本所在，其主要表现在如下方面：<br />
首先，政党利益的局限是南海丢失的关键。一个正常国家，国民利益是放在首位的，因为国民利益是国家利益的根本，保证了国民利益就保证了国家利益。但在中国绝非如此，中国是政党利益为核心。为了政党利益，国民利益和国家利益都不重要，甚至可以打着维护国家利益和国民利益的旗号，极端维护政党利益。所谓的“稳定”，不是民众利益和国家利益的稳定，恰恰是政党利益的稳定。在畸形的稳定下，才有“搁置争议，共同开发”的荒唐政策出台，导致他国在南海争夺上有恃无恐。南海疆域的丧失，就是国民利益的丧失。<br />
其二，高层大局意识观的欠缺也是南海丢失的主要原因之一。高层的大局观，其实是政党利益问题的延续。高层的核心任务，是保证政党的统治，维护其政权基础，其大局意识也难以跳出政党意识的局限，不可能站到国民利益和国家利益的高度上来。高层为了维护自己的权力核心地位，是不敢贸然在疆域争议上用兵。如果中国在南海上与外国动手，高层的权力核心就要受到动摇，转而移向军方权力核心。目前的高层军事把控能力是最薄弱的环节，一旦有战争，军方的权力核心势必就会凸显出来，动摇某些人的权力核心。于此，鄙人大胆断定，无论中国政府抗议多么强烈，以目前权力态势和政体机制，没有任何一位高层有决心一战的真实意愿。<br />
其三，目前国家没有真正的军队，南海的丧失是必然。中国的军队属于政党，说得直白一点，目前的军队是政党的保安，不是维护国家领土完整的利器。军队没有国家化，军队就不可能为维护国家主权和领土完整服务。军队存在的困惑，这也是体制因素的困惑。现在的军队除了透过政党向民众索取丰厚的福利，在保家卫国上，一直以来就停留在口号的强大上，而不是行动的强大上。同时，军队的封闭运行，中国军队的装备和实力到底怎样，不但民众是没有底，估计就是军方高层信心也不足，否则也不会一天老耍嘴皮子功夫。没有底气就没有打仗的决心和信心。<br />
其四，既得利益集团已经左右国家政权核心，围绕政党形成的既得利益集团，已经成为中国对外强硬的主要障碍之一。一旦发生战争，既得利益集团的利益必然受到最大冲击，利益集团在综合平衡下力求主和而不是强力主战绝非偶然。纵观中国历史，既得利益集团的如果没有受到外来威胁，是不可能主动出击的，只想守着一亩三分地不受侵害。当前对既得利益集团利益形成威胁的是国内民众，民众对利益再分配具有强烈的意识。一旦发生战争，受到冲击的既得利益集团的利益，有可能导致利益甚至其权力的重新分配，这绝不是既得利益者所期望的。历史上舍弃国土求安宁，这已经不是什么新鲜事。蒋公曾经提出的“攘外必先安内”，我们至今还坚定不移的执行着。<br />
其四，从国民心态与计生政策束缚了中国捍卫领土的手脚。很多人难以理解，为什么计生政策成为中国捍卫领土的短板，这与中国社会保障机制的缺失有密不可分的联系。国民对于保卫疆域国土，更多是来源民族主义和国家主义的原始冲动和激情，但是真要动手实干，这完全是两回事。多年来，由于国家在计生政策上未作调整和修改，大部分家庭就是一个子女，要把独子送上战场，国民心态就会有一个骤然转变。社会保障的缺失，孩子就是父母的基本保障，一旦失去孩子这一保障，国民的心态变化是难以预估的。无论战争的大小，给中国社会造成的整体冲击不可小觑，任何一个政治家都得权衡利弊。即使年轻人有心杀敌，其父母如何甘心孩子充当炮灰呢？<br />
其五，中国一直以安理会常任理事国自居，认为完全可以在安理会行使否决权，断绝南海周边国家从实际占领到法理化合法占领的梦想。其实安理会的否决权不过是中国维护南海疆域主权的自慰器，南海周边国家如果真把你当老大，还敢去占领吗？捏准你的就是缺乏勇气和政治体制的致命缺陷。你当你空头老大去，我拿到南海实际占有的控制权那才是真正的实惠。某些所谓的专家还在拿安理会那张否决权来意淫遏制南海疆域丢失幻想，不仅让人感觉其无知和愚蠢，更是透出中国政治的总体悲哀。<br />
南海疆域的丢失，在很长时间段是难以改变的现实。在这问题上，有大国博弈的因素，但主要还是我们自身的决策和中国内因的必然，造成南海的今日困局。威权体制，说白了就是强人机制，当有一两个强人出现时，这个国家就表现出超凡的能力，这就是当年毛泽东、邓小平为什么能够叱诧风云、笑傲江湖的原因。但是把一个国家寄托在某一两个强人的身上，这个国家随时都会处在危险之中。强人无法保证自己万岁，也无法保证自己永远清醒和正确，更无法保证威权的永恒持续。<br />
