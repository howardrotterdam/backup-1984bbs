													<h2>韩美西海军演激怒中国 军部媒体强烈抗议</h2>
						
						
																											<div id="postmessage_725493" class="t_msgfont">“韩美军演超出了底线”，“中国也可以进行军演，把进入黄海的美国航母视为假想敌”。<br />
<br />
在韩美西海联合军演临近之际，中国军部和官方媒体表示强烈愤慨。据悉，隶属美国第七舰队的“乔治-华盛顿”号航母（标准排水量9.7万吨）将参加韩美西海联合军演。<br />
<br />
《人民日报》旗下的《环球时报》6日发表社论说：“美国在公海军演的做法不触犯国际法。但美国这样做，对中国的安全构成了挑战。美国、包括韩国都不可能不因此付出相应的代价。”<br />
<br />
《环球时报》援引香港电影《无间道》的著名台词说：“出来混，迟早要还的。国际社会不是黑社会，但同样应该记住冤冤相报的道理。如果美国航母真来，中国人暂时忍的是怨气，而美国收到的是报应，因为报应是迟早的事。”前一天，该报还在5日头版头条敦促说，中国需要做出决断。中国大陆和香港的媒体还具体探讨了中国的报复措施。如，再一次在东中国海或黄海举行海上军演，以及试射被称为“航母杀手”的反舰弹道导弹 (简称ASBM)等。<br />
<br />
上月中旬在韩美两国为应对天安舰事件计划举行西海联合军演的消息传开以来，中国已屡次表示反对。上月22日中国外交部发言人秦刚在例行发布会上说：“（对韩美军演）表示关注。不能做加剧东北亚紧张局势，侵害地区国家利益的事情。”但那时的反对态度并不像现在这样强烈。<br />
<br />
上月30日中国海军开始在东中国海进行实弹射击演习，这期间中国军部出面表了态，中国方面的反应才开始变得激烈。1日中国人民解放军副总参谋长马晓天在接受香港凤凰卫视的采访时说：“中国的立场很明确。黄海与中国近海过于接近，强烈反对在这样的地方进行军演。”<br />
<br />
5日中国军事科学院世界军事研究部副部长罗援少将在凤凰卫视上说：“虽然反对美国航母进入黄海，但并不是怕它来。如果美国航母进到黄海，我们还可以把航母看做是演习对象――红蓝对抗中的蓝方，进行接近实战的演习。这将是一个了解美国航母作战能力的机会。特别是可以观察乔治-华盛顿号的C41SR系统（战斗指挥系统）、美军和韩军的通讯系统、美军航母编队以及航母的最佳作战阵位等。”<br />
<br />
中国的愤慨缘于中国把黄海视为关乎沿岸安全的战略要冲。也就是说，如果美国航母进入黄海，北京、天津等中国首都圈城市以及辽东半岛等地都将划入美航母的作战范围。罗援指出：“乔治-华盛顿号航母的作战半径为600公里，航母上搭载的战机的作战半径达到1000公里。而中国华北地区和大部分辽东半岛都将涵盖在这一范围。”<br />
<br />
日前，新华社《世界军事》杂志的总编辑陈虎通过一篇撰文主张：“此次联合军演号称针对的是北韩，但是美国航母进入黄海就是对中国本身构成的直接军事威胁。过去，美国在韩国的东海等海域同韩国和日本进行过联合演习，而此次欲在中国黄海展开演习，就是对华军事挑衅。”<br />
<br />
不过，专家们认为虽然中国的反应如此强烈，但不会采取相应的军事措施。北京外交界的一位军事专家说：“中国军部的强硬态度其实就是向韩美两国明确传达反对美航母进入黄海的信息。”<br />
