																											<div id="postmessage_315623" class="t_msgfont">杜绝“有偿家教”请先提高教师待遇<br />
2009-11-04 04:11:00　来源: 广州日报(广州)　跟贴 2 条 手机看新闻 周贵<br />
<br />
近日来，北京、山东省市拟出台“在职教师不得从事有偿家教”等规定，教育部也出面表示坚决反对在职教师有偿家教。<br />
<br />
对于“有偿家教”教育主管部门不宜“一刀切”。招致社会反感的是少数以职务之便故意从班上学生牟利的教师，上级教育部门可以根据现有《教师法》中“教师有执行学校的教学计划，完成教育教学工作任务；制止有害于学生的行为或者其他侵犯学生合法权益的行为的义务”的规定，对这些老师的行为处理，而无须为了这个而重新去出个新的法律规定。<br />
<br />
事实上，在现实生活中，绝大多数都是家长苦苦请求老师能够为他们的孩子进行家教。对于这类家长，他们不会主动配合政府去查处“有偿家教”。目前，义务教育阶段的老师的待遇普遍不高，不少教师的月收入只有三四千元，当他们的月收入连1平方米住宅都买不到时，要求他们不能利用自己的技能，利用自己的空余时间去改变自己的生存现状就有些过分了。而且在大学教授在外兼职讲课比比皆是的情况下，只对工资收入非常微薄的义务教育阶段的老师立法禁止课外时间兼职，似乎有失公平。另外只要教师不违反国家的法律法规，他们有权利自由支配自己的休息时间，无论他是用来休闲娱乐还是挣钱养家。学校都有自己完整的教师评价体系，如果教师评价达标，又何必去管教师休息日干了些什么？<br />
<br />
<br />
据笔者所知，深圳的老师月收入过万元，深圳老师进行有偿家教的现象就相对少许多，这里面除了有政府的严格监管，很大原因是因为老师收入已足够让他们过上比较宽裕的生活，这时候金钱的边际效用已经变低，所以老师宁可休息也不想去赚补课费，“有偿家教”自然不多。<br />
<br />
“有偿家教”的根源在于目前的应试教育，“有偿家教”的推动力主要来自于家长，“有偿家教”得以发展主要在于教师收入不高。当我们改革了目前的教育升学就业用人体系，大家不再这么追求分数，同时教师的收入得到大幅度的提高，即使政府不立法，“有偿家教”也不会滋长，也不需要教育部门加强查处。（作者为本报专家顾问团成员、广州卓越教育机构副总裁）<br />
