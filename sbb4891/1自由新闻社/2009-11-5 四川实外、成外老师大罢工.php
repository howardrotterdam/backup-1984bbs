相关背景2： <br />
<br />
冷静分析成都成外和实外罢课。 <br />
<br />
&nbsp;&nbsp;<br />
<br />
根据国家的统一安排，为了进一步增进老师的福利待遇，于是有了这样的一个文件。具体的情况落实到各大中小学是这样的：老师的工资有了大幅的提高，而且各种公积金，医保等福利项目的比例有了显著增加。当然，这都是针对公立的学校而言。每个公立学校的老师都是有教育局直属的“身份”的，而私立学校的老师并不完全享受国家的福利。<br />
事实上，实外，成外“罢课”绝不是一时的激烈冲突，我们可以看出这是长期矛盾积累而激化导致的必然结果。下面首先介绍下事情发展的始末：<br />
昨天，也就是2009年11月4日，在成外召开教职工大会，老师提到了关于“绩效工资”改革的措施，董事方面明确表示“你们老师的工资水平已经比其他学校高了，最多月工资再给你们加1000元”。1000元这个数目显然是远远低于国家教育局的标准的，而且实外的情况是（不知道成外如何，事实上情况也差不多）老师的医保，公积金等都被克扣到最低水品，而事实上这一大部分钱的走向，是被所谓“中间环节“所扣留的，也就是，这不仅是工资待遇的问题，更是老师们退休之后的生活保障，也是年轻老师买房，医疗等根本保障的问题！<br />
紧接着，今日，实外从下午的第一，二节开始进行罢课，各位老师在深秋的季节坐在楼下的天井，学生拥在老师周围，或在各楼层阳台上进行声援，场面较大。今日下午，实外决定停课，学生放假回家，”复课时间等待通知“。今日似乎有教委的领导与校方教师代表进行磋商，还没有有效地解决方案，各个方面正在协商解决措施。<br />
按照如此，后续的发展是实外，成外的附小等德瑞集团教育产业也将停课，将严重影响成都市的教育秩序，引发严重的后果。我们继续密切关注。<br />
在这里笔者发表一些看法：首先，该文绝非空穴来风，而是笔者进行了一些调查的结果。当然不是完全客观，但也不是”一小撮别有用心的份子“”煽动不明真相的群众“。对于一些观点，也希望大家正确看待。事实上，在2006年实外也闹过类似的事，教育局将实外的股权卖给德瑞集团，导致实外由”公立私助“的形式完全走向私有化，在此我们不探讨其中是否有何权钱交易等黑幕。当时实外的老师曾经找过教育局的相关领导，未果，事情也不了了之。据老师们说，今天成外也发生类似的状况，老师发现自己的档案不再属于教育局，而进入了”人力资源市场“的范畴。可以说，很多本来是属于国家系统的老师在这样不知情的情况下就被蒙着眼睛卖给了私人老板。<br />
学校股份归私人所有，老师本应该享受的国家福利（众所周知，国家给与教师的福利是很好的，老师的工资并不算高，福利对他们而言就很重要）得不到应有的保障，通俗说法就是，老师不再是国家的老师，而是被卖给私人老板变成了他们为了赚钱打出的”招牌“。<br />
我们没有其他目的，只是希望用一些微薄的方式声援我们所敬爱的老师，也为我们因为遭受无妄之灾的弟弟妹妹们尽一份绵薄之力，希望不要耽误了他们的前程。老师们没有钱，没有势力，只能在寒风里用法律给我们的”集会“的最基本的”权力“来保护他们最基本的”权利“。大家都看得见老师的辛苦，三伏天挥汗如雨，大冬天起早贪黑，认真批改我们厚厚的作业，并不仅如此，他们像我们的亲人关心我们，这些本不是他们的工资里包含的，也不是用金钱可以衡量的，他们都尽心尽力地为我们付出了生命最灿烂的时光。<br />
其实很多时候我们也是受害者，高昂的学费，落后的硬件设施，这些都是商业运作的副产品，六楼岌岌可危的板房，从来不开的中央空调，恶心的不可救药的饭菜，这些不是我们记忆中温暖的母校。我们真正爱着母校里是因为有那么多好的同学，更是因为有那么多好的老师，给我们无私的指导和关怀。<br />
希望社会能给老师们保障他们应有的福利。这次事件有一个圆满的结局。 <br />
