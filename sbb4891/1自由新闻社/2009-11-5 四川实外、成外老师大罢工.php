&nbsp;&nbsp;<br />
<br />
　　“哈佛女孩刘亦婷”母校老师停课向私人老板讨要人格尊严<br />
<br />
昨天，成都教育界爆出特大新闻：国内知名的成都外国语学校，这个培养了哈佛女孩刘亦婷和程婉心的学校，这个培养了7个高考状元的学校，老师们因不满权势资本的长期欺压盘剥，不满学校投资方老板和其老婆的谩骂和侮辱，不得以的情况下采取了罢课行动。全校300多老师在安顿好学生之后，来到学校大门口的地上静坐。全校几千名学生无一例外地支持自己老师的行动，他们在晚自习下课后在校园里高呼“老师加油”“老师，我们支持你们”的口号，场面让人心酸落泪。<br />
<br />
　　老师罢课，要求公司老板道歉<br />
<br />
罢课行动发生在5日下午2点后，起点则在4日。 <br />
<br />
4日，学校部分中干和老师代表与校长举行了一次座谈会，就绩效工资改革进行商谈。校方传达了投资方公司德瑞集团制定的绩效工资改革方案：老师人均上涨1000元，老师们对此表达了不同意见，并形成了书面文本。校方表示第二天（5日）请学校投资公司老板、德瑞集团老总严玉德来学校与老师解释。<br />
5日下午1点，严玉德和其第二任老婆王晓英（德瑞集团教育公司老总、成都市原某市领导秘书）来到学校，与学校中层以上干部开会，解释教师1000元绩效工资问题。该校是民办学校，未纳入政府绩效工资改革范畴，所以学校的绩效工资改革由老板自己决定。此前不久，严玉德曾经给老师们许诺，学校具有公办老师身份的老师，工资将比公办学校老师高出许多，民办身份的老师，工资也会上涨。<br />
“他不是来解决问题的，是来搞乱学校的。”一位老师这样说，严玉德一发言，就说这次人均上涨1000元是定了的，没有商量余地。“我手下几万学生几千老师，你们几百个老师不干，影响不了我，不愿意干的就走。”老师们提出，学校改革10年来，各种权益和福利得不到保障，该涨工资从来没有涨过，养老保险不按照国家足够金额缴纳，住房公积金少得可怜，只有几十块钱。“既然前不久老板说要大幅度上涨工资，就应该兑现承诺。”<br />
这时，严玉德老婆、德瑞集团教育投资公司总经理王晓英抢过话筒，骂道：你们这些老师，素质太低了，不配当老师。“你们愿意上课就上，不愿意上课就走。”<br />
这种带有侮辱性质的谩骂，激怒了老师们。他们纷纷要求严玉德和老婆给老师道歉。但王晓英和严玉德拒绝道歉，王晓英甚至变本加厉，对老师们更加恶毒地骂了起来。会开不下去了，严玉德看群情激奋，只好离开学校。<br />
3点左右，严玉德再次来到了学校。老师们以为老板会道歉，不想严玉德更加强硬地说：我不怕你们闹，一切后果自负。这样的结果再次让老师们感到了威胁和愤怒。他们当即决定停止上课，强烈要求严玉德给老师道歉。<br />
直到此时，整个校园没有一丝罢课迹象，学生们仍然在安心上课。为了不让学生受到影响，老师代表随即安排各班班主任回到教室，组织学生自习。学生们还不知道校园里发生了什么。<br />
下午4点，市教育局副局长崔昌宏和人事处负责人来到了学校。崔告诉老师们，由于成外是民办学校，不属于政府此次绩效工资改革的范畴。公司老板给老师们人均涨1000元工资，已经够可以了，希望和大家知足。老师们说，他们不关心绩效工资的问题，只需要严玉德和王晓英向老师们道歉。崔昌宏无法就严玉德的侮辱行为给老师们做出合理解释，只好离去。<br />
晚上6点左右，老师们罢课的事情终于传到了学生口中，他们纷纷走出教室，给老师们以支持。老师们随即把学生劝回教室上晚自习。<br />
随即，全体老师来到学校门口的空地上，开始了静坐示威，要求严玉德出面道歉。一些学生赶来围绕在老师们周围，支持老师的行动。看到自己尊敬的老师坐在地上，有的学生们流下了眼泪。老师理智地把他们劝回了教室。<br />
老师们表示，他们的维权行动，是合法的、有力有礼有节的做法，是在维护自己正当的权益。“老师们是知识分子，知识分子的人格尊严需要权贵资本的尊重，老师们的生存权利需要得到保护，老师们的辛苦工作需要得到权势的尊重。”<br />
与此相应的是，同属于德瑞集团的成都实验外国语学校全体老师也在5日下午3点左右罢课，该校全体学生放学回家，该校也是全国名校，培养了多位高考状元。成都外国语学校附属小学中海国际社区分校的老师，也在昨日罢课，声援其他老师。<br />
<br />
老师们申讨：要回我们的公办身份 <br />
<br />
其实，成都外国语学校这次老师罢课，是该校长期以来矛盾和问题积累的恶果。直接诱发点是绩效工资改革，但更深层次的原因，是该校10年前从公办身份转制为民办学校后，产权不明，教师身份不明、教师工作量超负荷但待遇低下、教师福利待遇低下、老师莫名成为银行不良贷款客户等原因造成的。<br />
该校10年前从公办学校转为民办学校后，有200多名老师仍然保留着公办身份，其余100多名老师是民办身份。但长期以来，公办身份的老师享受不到公办的待遇，住房公积金只有区区几十元，养老保险金没有按照国家规定足额缴纳。老师请病假要扣150元钱，请生育假没有一分钱工资。有的老师每个月只能领到500多元，个别老师病假期间，只能领到100元，连捡垃圾的都比不上。有的老师一个人要承担一个半人两个人的工作量，才能领到3000-4000元（其实月工资也只有1500-2000元），无法保证基本的生活，跟其他公办学校和民办学校老师的待遇根本没法相比，有的年轻老师长时间工作，连个人感情问题都没法处理。但在外人眼里，认为这所几千学生的学校，每年收取那么多学生，收取那么多学费（每个学生每年2.5万元左右），老师待遇是很高的。这种名不副实的待遇，让老师们很委屈和无奈。<br />
但即便如此，10年来，老师们仍然没有怨言，勤勤恳恳地工作，为学校培养出了哈佛女孩刘亦婷、程婉心，培养出了7个四川高考状元，无数的学生考上了国内外著名大学，成为国内著名的外语学校。<br />
“其实绩效工资对我们来说并不是最大的困惑，最主要的是我们的身份。”多位具有公办身份的老师告诉记者。“我们的劳动合同即将到期，要再次签合同了，但公司拿出的合同，没有像以前那样明确我们的公办身份。我们提出了质疑，公司随即又拿出另一份合同，说搞错了。”就是这样的一次“意外”，让老师们的怀疑再次加重。几年来一直有传言，说学校公办老师的身份已经被注销了，全部变成了民办。老师们一直不敢相信，但又没有确凿证据。“记得有一次老板严玉德在会上说，你们的公积金和养老保险都是我在出钱。如果我们是公办身份，就该国家出钱买养老保险，为啥子我们的养老保险要公司来出？这不是把我们公办老师的身份变成民办了吗？<br />
“我们现在到底是公办老师还是民办身份？”老师们表示，严玉德必须出来给老师们做出解释。<br />
昨晚近10时，记者还清晰地听到学生们在校园里高呼“老师加油”“老师，我们支持你们”的口号。据悉，老师们已经逐一通知学生家长，在6日一早来学校接回自己的孩子。部分家在成都的家长，则在5日晚上接走了自己的孩子。不少家长在接受采访时表示，老师们辛苦，物价在飞涨，确实该涨工资。希望投资方与老师好好商量，不要激化矛盾，影响学校的正常教学秩序。<br />
昨日，记者拨打严玉德和王晓英的手机，均处于关机状态。 <br />
