																											<div id="postmessage_315994" class="t_msgfont">卓星学长：学生应该以一个群体独立的姿态站出来发出自(2009-11-06 11:40:48)标签：杂谈&nbsp; &nbsp; <br />
<a href="http://blog.sina.com.cn/s/blog_62e046ac0100fe97.html" target="_blank">http://blog.sina.com.cn/s/blog_62e046ac0100fe97.html</a><br />
今天打了很多电话，我只能与大家分享我了解到的情况，尽量客观，或许有疏漏。请谅解。<br />
<br />
1. 成外老师分两种，一种是教育局下面的，那一批是老成外过来的，理应享受到政策带来的福利。一种是所谓的合同制员工，每三年签一次合同，我问了成外一个资深的老师，他说对私有制学校所有老师没有明确政策，但应该是参照执行的。所以，我想老师们既然集体决定行动，不会连这条都没有考虑到。所以，应该说是有理有据的行为。<br />
<br />
2.今天老师们在罢课的时候，出现了如下情况。学生们首先在班上上自习，后来出现了群体性的声音，诸如老师雄起，老师加 油之类的话。在我同老师打电话的同时能够清晰地听到背景传来的声音。老师们对学生的态度基本是一致的，尽量不让学生牵涉其中，也尽量不耽搁学业。罢课的时间选在半期考试的复习期间，也表明了老师的态度。<br />
<br />
3.牵头的老师据我了解主要有语文数学英语组的备课组长们，老师们采取了签委托书的办法， 委托了十几位老师作为代表来组织这件事。在今天下午的大会上面严玉德与集团一位副董同所有老师做了沟通，也有老师作为代表与集团对了话，但是由于严玉德和副董的态度和措辞让老师难以接受，于是老师们集体采取行动。<br />
<br />
4.具体措辞我想大家都应该了解，听来让人揪心。<br />
<br />
5.晚上的时候，在一位老师的指挥下，校旗和德瑞集团的旗在教学楼前广场被降下。<br />
<br />
6.学生，特别是高年级的学生对此事有清醒的认识，并且愿意采取行动帮助老师。我接到了素不相识的成外在读同学打来的电话，他们讲尝试过联系新闻媒体，但所有成都各大媒体听到消息后都说”知道了“然后挂断了电话。<br />
<br />
7.大部分学生被放回家了，返校时间待定。<br />
<br />
8.我尊敬的一位老师，副校长，在自己班上潸然落泪。他们是最难做人的群体。<br />
<br />
9.明天早上还有后续行动。<br />
<br />
 <br />
<br />
事实暂时告一段落，下面是一些个人观点。<br />
<br />
 <br />
<br />
 <br />
<br />
在这次事件里面，成外的学生，在校的和毕业的，希望大家可以以群体、独立的姿态发出声音。目的明确，是为了这件事情尽快有一个好的结局。<br />
<br />
那个最好的结局是什么。是按照国家规定的法规，《关于义务教育学校实施绩效工资的指导意见》，来解决这次的劳资纠纷。是为了我们的老师不用每年只领9个月的工资，有足够的三险一金缴纳，还有，同全中国所有的在从事义务教育工作的人民教师一样，有不低于当地的公务员的工资水平。<br />
<br />
 <br />
<br />
 <br />
<br />
为何要群体。难做人，学生更难做。每个人的声音都是那么的渺小。即使我们在人人改掉千百个状态也无济于事，但贾君鹏就不同了。有足够数量的积累就会有足够的关注，这是不争的事实。如果现在成外的五千个学生站在一起，再加上我们散落在各地的人汇集成江河，还有背后的各种资源，至上而下的或是至下而上的，这样的声音才能被所有人听见。<br />
<br />
 <br />
<br />
 <br />
<br />
为何要独立。我问一个老师，学生们如果有意愿来参与到这 个事件中来，你们希望是以什么样的方式。他想了想说，希望学生是一个自发的行为，而不要让外界误解是老师藉着情感因素和动机操控和利用了学生。这件事如果解决不好，学生的利益同样受到损害。有传言，严玉德说如果给老师加工资，就多收学生每年五千块钱。我还问，也就是说，老师们不会反对有这样的学生自发行为。他说，内心支持，表面或许会。我希望所有学生，曾经被剥削过和正在被剥削的，可以从学生的角度，以另外一个利益集团的身份参与到这个事件。<br />
<br />
 <br />
<br />
我查了一下，这个问题不是成外，实外，全国各地的老师都遇到了类似的困境。或许一个成功的范例能让这个本来可以惠及千万人但因地方政府和各种势力的不遵守和干预造成的政策失效有更好的解决力度和措施。<br />
<br />
 <br />
<br />
中国太大了，山河虽大，我们所能够守护的只是我们的一隅。而且要做到已是不易。<br />
<br />
我以为，我们为什么愿意发出我们的声音，是因为我们对这片土地有深深的眷恋。<br />
<br />
曾经教过我的老师，曾经一起奋斗的同学，成都西三环外的那方校园，我为你们而写。<br />
<br />
