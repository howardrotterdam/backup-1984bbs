&nbsp; &nbsp;&nbsp;&nbsp;大家好！我叫庾琳，是长岗中学八（１）班的班主任。今天，我不是来谈什么经验，而是来向大家学习的。我自认不是一个优秀的班主任，跟在座的很多班主任相比，无论是敬业精神还是管理经验我都自愧不如，要我来谈班主任经验实在有点班门弄斧的味道。但是，这一路走来，我遇到了不少难题，也经历了很多挫折，成功的喜悦，失败的无奈，有欢笑有泪水，更多的是感动。下面我来谈一谈在班级管理中的一些不成熟的想法和做法，希望能得到大家的指正，并希望能引起大家对班级管理的深思。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;班主任是班级管理的灵魂，班级的好坏与班主任有直接的关系。若要当好班集体领导者、组织者、管理者的角色，就必须探索科学的管理方法和建立高效能的管理体制。<br />
<br />
一、勤用脑，少动手――放开手，让他们自己走<br />
<br />
&nbsp; &nbsp; 刚开始做班主任的时候，每天忙得连坐的时间也没有，但效果并不理想，这样做班主任的效率是不是太低了？难道做班主任就一定要做到鞠躬尽瘁、死而后已吗？我还年轻，才不愿意过早地步入衰老的行列。如果我只要动动口甚至不动口，就能让学生井然有序地动起来，是一件多么美好的事情。培养一批能把大部分工作分担出去的班干部是非常必要的。班主任、班干部和学生三者之间的关系如同伞柄、支架和布，一个再好的伞柄如果没有支架的支撑是无论如何也撑不起一片晴空的。<br />
<br />
&nbsp; &nbsp; 正所谓：疑人不用，用人不疑。我的班级管理要诀是勤用脑，少动手。对一些举手之劳的事情，我从来不插手，也不过多干予，放权让班干部们去组织、去领导、去管理、去落实、去想对策，让他们放心地进行大胆的尝试。而我只是偶尔过问一下，在适当的时候给予一些点拔，平常多表扬、多鼓励，便轻轻松松地坐看班级管理日益有条不紊。而学生们也能在实践中施展自己的才能、充分发挥自主性、磨砺自己，脱胎换骨做班级的“小主人”。<br />
<br />
&nbsp; &nbsp; 然而，作为学生，学习是主要任务，不可能也不合实际花大量时间来进行管理工作。因此，管理工作不能只让几个人来做，要让全班同学动起来，我不时给班委会加入新鲜血液，让更多学生得到锻炼的机会。每个人都是管理者，每个人又都是被管理者，从管理与被管理的角色转换中寻找最佳的定位，让学生成为班级的主人，班级的事成为大家的事，班级的荣誉成为大家共同的荣誉。<br />
<br />
&nbsp; &nbsp; 我还要求班干部们做到事事有总结，无论是取得了成绩还是以失败告终，有活动就有总结，我自己总结，学生也要总结，总结经验，吸取教训，明辨是非，积累宝贵财富。即使没有惊天动地的伟业，但也让自己走过的每一段路，留下的每一个足迹，都能折射出生命的价值，才能让自己走过的路不至于白走，让自己付出的努力没有白费。<br />
<br />
二、做学生一辈子的朋友<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;在担任班主任工作的实践中我深深体会到：要想把一个班集体带好，并且使每个学生在德、智、体、美、劳诸方面全面发展，必须做到“知己知彼”，也就是要了解每个学生的心声，知道他们每天都在想什么，在学习中遇到哪些困难，在生活中遇到哪些麻烦。只有对学生了解得非常透彻，切好每个学生的脉，对“症”下药，因材施教，做到百战百胜，不让一个学生掉队。<br />
<br />
&nbsp; &nbsp; 因此，我经常亦师亦友地和学生在一起，无拘无束地聊天，以“朋友”的立场去聆听他们的心声，为他们的烦恼出谋划策，让他们感到老师是真的在“用心”去关心他们。使他们主动和我接触，向我敞开他们的心扉。于是，我在最短的时间内，摸清楚了班上每一个学生的性情，找准思想的突破口，对他们进行思想教育。这样，在说说笑笑中，整个班集体的情况我就了如指掌了。<br />
<br />
三、“塑健康人格，铸健康人生”――做学生心灵的导师<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;近年来我校一直注重学生的心理健康教育，致力于塑造学生的健康人格，作为学校的心理辅导员及省级课题《当代青少年健康人格研究》的子课题《中学健康人格教育课程与教学模式研究》的开发组成员，我十分重视学生健康人格的塑造，在不断摸索、探讨与实践的过程中，加强自身的辅导能力。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;这学期我班从外校转入一名女生，该生被原学校劝退后转入我校，进了我班。还未见其人，我便早已久闻其大名，这个别人眼中的“问题学生”、“大姐大”劣迹累累，实在令人头痛烦恼。开学不到一个星期，她便已旷课十七节，迟到、无故早退更是家常便饭。科任老师纷纷向我投诉，该生在上课时除了睡觉，就是在用手提电话与人互发短信，还跟同学传纸条、打眼色，大大地活跃了课堂的“气氛”，也陆续将一些同学收入其“门下”。科任老师们都主张严厉惩处该生。我明白她所做的一切，只是在向我这个班主任示威，她在告诉我，最好别去“惹”她！说实在的，当时我也非常气愤，如果老师能动手打学生的话，那我第一个就要把她揪出来痛打一顿！可是，打完之后呢？如果“打”跟“骂”就能解决问题的话，那么，她也不会来到长中成为我的学生。于是，我选择了冷处理。我让班干部们留意她的一举一动并做好记录，随时向我汇报。我耐着性子不动声色，倒要看看她还能搞出些什么花样。然而，她却留下纸条离家出走了！她的母亲打来电话跟我长谈了将近两个小时，我终于明白她为什么会如此叛逆了，也看到了让她转变的希望。接下来的日子，我事事对她的喜怒哀乐感同身受，去体验她的欢乐和痛苦，陪她一起哭一起笑，跟她分析社会现实现今形势。慢慢地，她向我敞开了心扉，一些不愿说出口的心里的话也都愿意跟我讲。她上课专心了，也不旷课了，校运会时她积极主动参与，受了伤挂了彩还埋怨自己不小心，直说抱歉不能参加接力赛。她的母亲反映，原本夜生活丰富的她现在也经常呆在家里陪伴母亲，就算有电话打来找她，她也会说明天要上学不出去了。中段考时，该生带病坚持参加考试，并获得了优异的成绩，在班里排名第十，年级排名六十一。看着她兴高采烈的天真笑容，我知道自己的任务还未完成，离真正成功还有一段很长的路程，我会坚持的！<br />
<br />
四、学会批评<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;记得刚做班主任的时候火气是比较大的，很容易在课堂上发脾气，有时弄得自己很难堪，甚至下不了台，而且也很难让学生服气。后来我不断地反思和探索，摸索出一些行之有效的招数。有一招叫做：“退三进一”。我们看电视时经常可以看到这样的镜头，老虎等凶猛的动物在进攻前首先会向后撤退。为什么撤退呢？是为了更好的向前进攻！<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;在批评学生一个缺点之前，不妨先表扬他三个优点，此谓之“退三进一”。我学会了善于捕捉学生身上的闪光点，用放大镜去看他们的优点，用缩小镜去看他们的缺点。从心理学的角度看：一个人只要体验一次成功的欢乐，便会激起追求无休止的成功的力量和信心。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;我们班有个男生，性格比较反叛，说他什么都不乐意，做什么他都认为不对！但是他善于长跑，口才好，反应快。在一次美术课巡堂的过程中，我发现，他不仅画画得好，而且有着超人的想像力和创作力。这是意外的收获！于是我抓住这个契机找他谈心，我先由衷地祝贺他在校运会男子3000米赛里取得了优异的成绩，然后感谢他为班争光，接着又拿着他的画，真诚地赞扬他的创作力。在他面露笑容的时候，我突然问道：“但是你有没有好好想过，你那么有才干却没有一个人选你当班干部，你有冷静地思考过那是为什么吗？”然后我跟他一一分析原因，过了一段时间又找他长谈了一次。慢慢的我发现这个学生在学习态度及与同学相处方面有了很大的改观。于是我起用他做班干部，安排他做班级黑板报的主编，并且担任体育委员，他做得很出色，学习成绩也有了很大的进步。五、适当的时候“小题大作”或者“大题小作”<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;为了营造一个班风纯良、学风浓厚的环境，在班级日常管理中我时不时会“小题大作”或者“大题小作”，正确引导班级舆论，让班级舆论形成一股无形之力时刻鞭策着学生。如校服的拉链要拉到什么位置才算符合标准这样的小事，我也会把它化成大事在班里从严要求，一丝不苟地要求学生严格遵守，培养良好的日常行为习惯。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;我班一位外地学生小林，某天傍晚六点半左右骑车回家，发现一名小女孩在马路边哭泣，便上前询问，原来小女孩迷路了。于是他马上帮忙联系小女孩的家人及老师并陪同她一起等待家人。做完好事该生并没有到处去宣扬，直到小女孩的家人找至学校，我们才获知此事。这可能只是一件举手之劳的“小事”，但于小女孩一家来说，这可是件天大的喜事，于是他们送来了锦旗及感谢信，还请了电视台的记者前来采访报道。一时之间小林成了长岗中学的“大英雄”、“大红人”。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;说句实在话，我也为有这样的学生而感到自豪。但是，不能让小林一直陶醉在此事当中，乐昏了头好事就会变成坏事了。于是，我采取了低调的处理办法。表扬是一定的，向同学们宣扬好人好事，抓住契机教育每一个学生，然后转而跟学生们共同探讨当遇到事情的时候，我们该怎么办？校内议论纷纷，可是班内却如同往池子里扔了块小石子，激起的波澜很快就平静了。于是大家便觉得，这种事情对于每个人来说都是理所当然的，这是我们的义务，更是我们的责任。小林也很快地平静下来，他做好事并不是为了想出名，他认为这并没有什么大不了的。而且他还冷静地作了反醒，当时他实在太不够镇定了，只急着联系小女孩的家人，却忽略了小女孩应该已经饿了，他却没有买些吃的给她。让好人好事成为理所当然的事，让正直善良深入每个学生的心里。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;总而言之，我的班主任工作理念是：小事放手做，大事从旁助，扣开学生的心门，多赞美多鼓励，先表扬后批评，适时小题大作，大题小作。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;我的发言完了，谢谢大家！<br />
<br />
2006年12月</div>

							
							
							
							
