													<h2>前中国国家主席等遭西班牙法院传讯</h2>
						
						
																											<div id="postmessage_344131" class="t_msgfont">西班牙国家法院的一名法官下令，就法轮功学员在华遭受酷刑折磨案，对前中国国家主席江泽民等人提起公诉。中国人权律师说，西班牙法院的这项行动具有重要象征意义。马德里的教会人士介绍了西班牙的宗教自由环境。<br />
<br />
*群体灭绝罪传讯*<br />
西班牙马德里国家法院法官伊斯梅尔・莫雷诺，在初步听取法轮功学员在中国所受酷刑折磨投诉后，下令前中国国家主席江泽民等人接受法庭讯问，以确定他们是否犯有群体灭绝罪和酷刑罪等重大罪行。据报导，被起诉的中国官员还有罗干、薄熙来、贾庆林、吴官正等人。<br />
如果认定有罪，上述人员将面临至少20年监禁。报导说，法院很快向上述人员寄出法庭文件，并且要求他们六星期内答复，法官如果没有按时收到答复，即可下达逮捕令。<br />
<br />
*法轮功案的里程碑突破*<br />
杨森是美国中部地区法轮大法协会主席，他说，法轮功学员在马德里的胜利是长期斗争的结果。<br />
他说：“这个案子进行了大概已经有两年，被告都是现任和前任的中共高官，这些人是迫害法轮功负有直接责任的人；法轮功原告有15个人，他们来自世界各地。”<br />
<br />
*西班牙的司法环境*<br />
西班牙法院为什么能够受理对中国领导人的法律诉讼？戴维・圣地亚哥是西班牙马德里国际教堂牧师，他说：“我认为，西班牙宗教自由以法律为基础，西班牙广泛宗教自由是总统所领导的政党实现的。”<br />
报导说，西班牙马德里法院受理法轮功学员遭受酷刑案依据的是所谓“普世司法管辖”原则，按照这一原则，西班牙2005年以来不断受理世界各地种族灭绝案和侵犯人权案的调查。西班牙法庭曾经以践踏人权罪成功引渡并且审判过前智利独裁者皮诺切特。<br />
<br />
*象征意义*<br />
不过，国外法院诉讼能否在中国执行还有待观察。江泽民等被告对马德里法庭要求不予理睬，很可能国外出行前往跟西班牙有引渡条约的国家时被引渡到西班牙。北京的人权律师唐吉田对美国之音说，马德里法院的裁决更多的是象征意义。<br />
他说：“这项裁决具有象征意义，也就是说，提醒中国大陆，不管是决策指挥实施侵犯人权的人，还是具体执行这些命令的人，一定要有清醒的认识：即便在国内法暂时得不到惩处，但是从普遍管辖角度，肯定要追究相关人的责任；很多人过去执迷不悟，现在一定要有清醒认识。”<br />
<br />
*宗教迫害的原因*<br />
马德里国际教堂牧师戴维・圣地亚哥同事表示，依据他的经验，受迫害的宗教团体往往可能会被认为持某些非宗教目的，或者涉及其他法律方面的问题。<br />
