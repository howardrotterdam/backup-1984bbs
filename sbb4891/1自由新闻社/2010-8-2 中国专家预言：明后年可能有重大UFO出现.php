　　“外星人肯定有，只不过需要人类进行漫长科技探索。”中国科学院紫金山天文台研究员王思潮说，“银河系有两千亿颗像太阳一样的恒星，整个宇宙有数不胜数的像银河系一样的星系。有外星人的概率太大了。”<br />
<br />
　　星期柒新闻周刊：您觉得神秘的外星生命存在吗？<br />
<br />
　　王思潮：外星生命肯定存在的，这种可能性占到99.9%，而有智慧的外星生命――外星人很可能存在，这种可能性占到90%以上。比如火星上以后有希望发现细菌病毒，甚至更高级的生命。就算火星上找不到，太阳有两千亿个兄弟姐妹，一定会有生命存在。地球上有生命的历史目前至少已经有38亿年，也就是说在46亿年大部分是有生命的。而有智慧生命，也就是人类的出现也只有几百万年的时间，这在地球的历史长河中占到很少的一部分，因此银河系里有外星人的可能性相当大。就像以前人们只知道在亚洲、欧洲有人类，但并不知道美洲也有人，这是逐步发现的过程。<br />
<br />
　　星期柒新闻周刊：您觉得人类跟外星生命会见面吗？<br />
<br />
　　王思潮：有可能会面。会面的难度要比取得联系要难一些。可能会通过无线电或其他手段发出信号，我们收到了，给他们回复会面有可能，广义的联系可能性大一些。但是见面比较难。<br />
<br />
　　星期柒新闻周刊：为什么会面比较难？<br />
<br />
　　王思潮：举个例子，有一个星球距离我们100~200光年。目前人类最先进的飞行器每秒飞10公里，那么到这个星球可能需要几万甚至几十万年的时间。同时要考虑，人类要去其他星球，生命怎么保证安全，宇宙的线的辐射伤害等等。也许外星人的水平比我们高，一秒钟能跑10万公里，要比我们快一万倍，他们来比较容易，600年就会来了，但这也不是那么容易。<br />
<br />
　　星期柒新闻周刊：外星人的飞船会造访地球吗？<br />
<br />
　　王思潮：我个人认为，外星人的飞船是有可能造访地球的。认为不可能的观点是建立在以当今人类科技水平去想象外星人。外星人比我们现在先进十万年，也是很平常的事，他们为什么就不可能派飞船造访地球呢？而且他们并不一定要自己来，可以派高级智能机器人驾飞船前来。<br />
<br />
　　星期柒新闻周刊：外星人用什么语言和我们交流？<br />
<br />
　　王思潮：最简单的东西，就是数学语言，二进制。也就是说是0和1，外星人通过这些脉冲信号发过来，有脉冲的地方代表1，没有脉冲代表 0，人们可以根据这些数字变成两个质数的乘积，比如23乘以19，根据答案排成长方形的图案，这个星球就能用天文的方式显示出一切讯息，比如星球的位置，是什么样子，有外星人的种类。初步联系后，还会把那边的音乐图片、语言发过来，但这些都是一步步的事情。<br />
