<br />
　　2008年5月4日，中共中央政治局常委、国务院总理温家宝应学生们的热情邀请来到中国政法大学，与大家一起共度青年节。这是温温家宝在学校图书馆阅览室与学生们亲切交谈。&nbsp;&nbsp;新华社记者姚大伟摄<br />
<br />
<br />
　　编者按<br />
<br />
　　2008年5月4日，中共中央政治局常委、国务院总理温家宝应学生们热情邀请，来到中国政法大学，与大家一起共度青年节。在政法大学法学图书馆二层阅览室，温总理与学生们围绕依法治国亲切交谈。在12月4日全国法制宣传日来临之际，本报特全文刊登温家宝总理同中国政法大学学生的谈话，以飨读者。<br />
<br />
　　温总理：去年10月份，我收到政法大学全体师生的信，邀请我到政法大学来，让我走一走宪法大道，看一看明法楼，看看同学们。我一直惦记着这件事，今天早晨我又把这封信翻出来。我订了计划，每年“五四”都到一所学校。你们的信里都给我罗列出来了。从2003年起，“五四”都到了什么学校，还没到政法大学，然后就讲依法治国的重要性。所以，今天我就来了，同刘延东同志一起来看看同学们。<br />
　　学生：我们很想念您。谢谢总理。今天终于美梦成真了。<br />
　　温总理：其实要说法律的重要，怎么强调都不为过。因为整个国家的建国方略就叫依法治国，这是从大处讲。从每一个老百姓切身的生活来讲，也要靠法律来保护他们自由的权利，保护他们全面发展的权利。对于一个社会，如果说发展经济、改善民生是政府的天职，那么推进社会公平正义就是政府的良心。公平正义当然包含很广，从收入分配到教育权利，但是很重要的一点就是司法制度。就是立法必须要做到公，执法必须要做到平。<br />
<br />
我今天一出门就有个感触，我的汽车刚拐弯，就被两个上访的群众拦住了。我平时有个要求，就是无论在什么地方，只要群众把我的车拦住了，一定把信接过来，然后妥善地给他安排和回答。所以，我一上楼到办公室，就问警卫人员，他反映的是什么问题。他们说是前几年他一个家人被车撞死了，应该按刑法处置，而现在只作赔偿处置，他认为执法不公，他为这个已经上访八年了。这在我们看来可能只是一桩小事，但是在群众看来，就是大事。他可以为他的亲人判决得是否公正上访八年，甚至有的还更多。我统计了一下，在我住的周边，经常来上访的47%都和司法有关，而且很多都是为自己亲属或者本人判决是否公正上访的，好多是多年上访。你们都是学法律的，我有时在想，什么时候我们的法律，从立法的完备到执法的公正，到诉讼法的健全，使人们不必跑这么远，能够就地解决这些问题，那时我们的社会就更进步了。因为所谓上访，它所体现的不是法律的程序，还是寄托在人，寄托在领导人的批示，应该说在某种意义上还带有人治的封建色彩。在一个现代化的国家，应该依靠法，应该依据法律来解决这些问题。<br />
<br />
你们去年10月份的信写得很深刻。今年是我国提出依法治国方略十周年，这是党的十五大提出来的。当时我负责组织文件起草工作，关于用“法制”还是“法治”是经过再三斟酌的。虽然一字之差，但意义大不相同。因为光讲制度的“制”，只提到制度建设这个层面；而治理的“治”，就提到了治国的层面。中央作出了正确的决定。完整的提法就是我们今天所说的：依法治国，建设社会主义法治国家。今年正好是十周年。<br />
　　学生：总理，您今年是第一次来到法大，请问您对法大的感受如何？您对中国政法大学的发展又有何期待？<br />
　　温总理：我刚才说了，我们已经把法律提到治国的层面，因此培养法律人才就是十分紧迫而具有长远意义的任务。中国政法大学是我国培养法律人才的最高学府，承担着重大的责任，可以说任重而道远。为了加深你对这个问题的认识，我想说我的一点考虑。依法治国，建设法治国家，是建设中国特色社会主义事业的重要内容，而且是成熟的社会主义的标志。要从这个高度来看问题。法治是基础，我们常讲经济发展、社会和谐，其实都离不开法治。如同真理是思想的首要价值一样，公平正义就是社会和一个国家的首要价值。最大的公平正义，除了在民生、在教育，很重要的是在法律。<br />
　　你们在信里不断地写到法治精神这句话。什么叫法治精神？咱们一起概括一下。你们学校的门口有块石头上面写着四个大字，就是“法治天下”。<br />
　　学生：对，法治天下。<br />
　　温总理：“法治天下”就是法比天大。我把它演绎一下：法大于天，法治天下。这是很形象的一句话。我觉得这句话抓住了法治精神的核心。这就是：第一，宪法和法律的尊严高于一切。第二，在法律面前人人平等。第三，一切组织和机构都要在宪法和法律的范围内活动。第四，立法要发扬民主，法律要在群众中宣传普及。第五，有法可依、有法必依、执法必严、违法必究。“天下之事不难于立法，而难于法之必行”。有法不依，不如无法。<br />
　　学生：总理，我想冒昧地补充一句。有句话是这么讲的，就是说法律的公平正义不仅要实现，还要以人们能够看得见的方式来实现，我想这对于我们安定团结的局面也是非常重要的。<br />
　　温总理：你补充得很对，就是法律的公平。你看我们法院总弄个天平放在那儿，刚才我讲了什么是“公”，什么是“平”，就是立法须公，执法须平。公平正义在法律上是最为重要的，它的反面就是徇私枉法。我们要建立一个公平正义的社会，就必须做到这个要求。<br />
　　学生：总理，您说的法治精神和我们的校训是一脉相承的，我们的校训是“厚德、明法、格物、致公”。<br />
　　温总理：这几句话我记住了。厚德，就是要不断培养高尚的道德情操；明法，就是要掌握全面的法律知识并依法办事；格物，就是要善于分析问题，弄清事物的原理和性质；致公，就是要一心一意为社会和大众服务。其实，我们不但要明法，还要明道，这两个又是统一的。明法就要懂法，明道就要懂道理。前者是法律的约束，后者是道德的约束。法律与道德的关系，也是法治精神的一个原则。<br />
　　学生：总理，我们都知道民生问题是您一直倡导和关注的问题，我们学法律的也知道一句话：“民为邦本，法乃公器”。在今年人代会的报告中您也提出，改善民生要通过法治的途径来解决，对于这一点我们想听听您的看法。<br />
　　温总理：在上一届政府，也就是我2003年刚担任总理的时候，提出政府工作的三项原则，叫做科学决策、依法行政、民主监督。依照法律，我们要接受人民代表大会的监督，接受人民群众的监督、新闻媒体的监督、民主党派的监督。我们讲的是行政权，当时我们就把依法行政摆在非常重要的位置。“非典”之后，我们很快就着手制定了《全面推进依法行政实施纲要》。我认为，无论做总理的也好，还是做一般干部的也好，脑子里考虑的是如何把为人民服务的思想通过法律变为现实。也就是说，从处理一般的民间事务到治国理政，都不是靠人而是靠法。我们说，制度具有根本性、长期性和稳定性。其实法律是成熟了的制度上升而来的，因此它更具有根本性、长期性和稳定性。我举个简单的例子，我看到学生们上学经济上有困难，就想到要给同学们增加助学金、奖学金，这是在政策层面。我们将来要从政策层面上升到立法层面，就是要建立国家奖学金助学金制度。2006年中央财政用于全国普通本科高校、高等和中等职业学校的奖学金和助学金支出是18亿元，2007年大幅提升到95亿元，今年将安排190亿元，如果加上地方财政支出则达到308亿元，所以现在你们有好几种奖助学金，叫做奖助学金资助体系，但这还不够，将来可以先上升到国务院条例，以后还可以立法。这就是刚才这位同学讲的所谓为民和法律的关系。任何好的思想其实都是从实践中来的，但是如果简单制定一个政策性文件，是不能保证长期实施的。我们解决民生问题的各项政策，只有当其成熟上升到法律制度，才具有长期性、根本性和稳定性，才能保证人民长期受益。<br />
　　学生：总理，我有一句话想说一下，就是这里大多是学法律的，但是我想要做好我们中国的法制事业不能光靠法律专业的学生。我是我们学校第一届法制新闻专业的研究生，我们都简称自己是“非法”专业的学生。正赶上我们第一届19个同学毕业的时候，您就来到了我们政法大学，对我们刚开设的专业是一个非常非常大的激励。我们“非法”专业的学生在中国政法大学虽然是少数，但我们是一个把传播法律文化、弘扬法律精神作为自己使命的专业，希望您给我们提一些寄语，给我们一些更重的担子让我们以后去承担起来。<br />
　　温总理：我觉得法律应该包含三个体系，一个是立法，一个是执法，还有一个是普法，这三个体系就决定我们中国政法大学要作为三个中心：一是要成为培养法律人才的教育中心，二是培训法律干部的培训中心，三是全民进行普法教育的普法中心。所以，你所学的专业也不是“非法”的。<br />
　　学生：总理，我今年准备要读环境法专业博士，环境法也是中国政法大学的优势学科，我知道您对环境问题也很关心，希望您给我们一些寄语和期望。<br />
　　温总理：我们国家正处在发展时期，要做到经济发展与环境保护并重，就必须加强立法。我们现在采取许多保护环境的措施，但是环境立法还远远跟不上形势的要求。举一个很简单的例子，我们建立了许多污水处理厂，国家花了很多钱，但是这些污水处理厂建成以后不能正常开工，为什么呢？因为消耗成本高。有时为了应付领导，在参观的时候把污水处理设备开动一下，但是领导走了以后，污水还是直接排放到河里、田地里。这光靠教育是不行的，必须靠法治。<br />
　　教师：在您担任总理后的2004年的时候，制定了《全面推进依法行政实施纲要》，您专门提出要对立法进行成本效益分析。我们“法与经济研究中心”是法大刚成立的，是对立法进行成本效益分析的一个研究单位。据我了解，在国务院的一些部委已经开始立法经济效益分析，包括地方像海南省也在进行。请您对我们这个新专业提一点希望。<br />
　　温总理：这是你对我们提出希望。我们的立法、法规，之所以耗时长、效率低，常常是因为我们的部门职责不清，推诿扯皮。往往一个条文，不同的部门有不同的解读，不同的要求，可以纠缠两三年，效率怎么能提高？作为行政立法，必须跳出部门这个圈子，站在全局的高度。提高立法的效率，根本不在工作本身，而在体制。<br />
　　学生：借着您这次来我们学校的机会，我想为我们学校说几句话，希望您能听进去。我是本科就在政法大学读法学的，七年来我是一点点看着学校各种设施的变化，学校越变越好，是深有感触的。您在依法行政会议上就提出建立法治政府这样的一个工作目标。这些年我们也感触到了，我们的国家正在走向法治之路，这对我们青年学生是非常有鼓舞作用的，也激发了我们法学专业学生的学习热情。但在法治教育、法学教育这一领域内，我想政府对我们政法大学的投入、建设还不够。您可以看看我们的书桌、座椅、我们的图书馆外部环境，还有对面教学楼、办公楼。虽然我们可以很骄傲地说我们是一流的政法大学，但是我们的硬件设施离一流的水平还有差距。<br />
　　温总理：我接受你的批评。在任何国家，法律专业、法律学校，都是受人尊重的，而且具有很高地位。特别是大部分学有成就的优秀法律人才都进入国家司法部门，他们手里都掌握着很大的权力，因此学校这个阶段对他们的培养十分重要。政府将更加重视法律教育，加大对政法院校的投入，为培养更多优秀的杰出的法律人才做好服务。<br />
　　学生：温总理您好，我是法学专业本科三年级的学生。我想问您一个问题：您曾经在很多场合多次提到，对青年学生要特别关心，而且提出过“三要”的要求，一要有理论联系实际的学风，二要有追求真理的精神，三要有对国家对社会对人民强烈的责任感。而学以致用对于我们学法的同学是极其重要的，强烈的社会责任感也是扎根于我们每一个法律人的心中的。近些年咱们国家提出了大学生下到基层做村官、鼓励大学生自主创业等很多政策，希望您在这方面谈谈对于政法大学的学生有什么更高的要求或者期望？<br />
　　温总理：这里我想讲一个问题，就是法律与人的关系。我不知道你们有没有人研究这个问题。因为法是人制定的。当然随着社会的发展立法越来越民主化，就是要让人民来参与，但是专业的法律人才还是离不开的，需要由法律专家把老百姓的意见、要求，用法律的语言表述出来。法也是靠人来执行的，你们将来有的要做法官，要审案、要断案、要执法，能否做到公正十分重要。对于一个学法的人来讲，最重要的就是要有对国家、对人民、对社会的高度责任感，要有一颗公正的心，要爱我们这个国家。这不仅是做人的良知，而且是你们由于懂法的要义而产生的对自己的终生要求。这要从一点一滴做起，首先就是对国家、对人民要有感情。我这个人有很多缺点，但是有一点我是不甘落后的，就是爱我们这个国家。每一滴血液，每一个细胞，甚至将来死了烧成灰，每一粒灰烬都是爱国的。我们今天讲爱国，应该包含三层意思：第一，要自强不息。为中华民族的崛起，为了振兴中华要自强不息。第二，要开放包容。只有开放兼容，国家才能富强，这是一个大国的胸怀。第三，要艰苦奋斗。为了我们国家的富强，要几代人、十几代人、甚至几十代人埋头苦干，努力奋斗。对人民要有一颗爱心，这对政法人员尤为重要。我刚才讲了道德，其实说一句很简单的话，比如我们常说的同情心。同情心是道德的基础。就是说你要时刻想着人民，想着我们的国家。由于我们国家发展很不平衡，像西部地区、像农村，还有好多人生活得不好，所以这就要求我们自己努力学习，学好本领将来为人民服务。我觉得一个学法律的人，对自己的道德品质和人格的要求必须更高。<br />
　　学生：我跟导师主攻突发事件应急管理。去年11月出台了《突发事件应急法》，就是在国务院有关条例基础上制定的。<br />
　　温总理：应对突发事件的法制建设至关重要，这次雪灾就暴露出来我们在这方面还准备不够。什么事情都得靠实践检验，如果有法律规定了，平时再加以训练，一旦遇到突发事件就知道怎么应对。没有健全的法律体系，应对突发事件就找不到头绪，手忙脚乱。<br />
　　学生：总理，我主攻西方政治思想史，同时我也对中国古代政治尤其是先秦的政治思想，如孔子、孟子也比较感兴趣。我觉得一个国家的思想文化也很重要，我在研究西方思想史时又看了诸子百家，在认识自己的基础上借鉴别人，对两种不同思想进行比较，还是有用处的。<br />
　　温总理：诸子百家时期算是奴隶制吧，公元前536年中国已经有了“铸刑书”，当时的刑法已经铸鼎了，就是说法律要明示，要让每一个人知道。西方的法，可以追溯到公元前450年，就是《十二表法》。再往前还有吗？<br />
　　学生：再往前，柏拉图时就注重法律了，到亚里士多德进一步发展，罗马时比较兴盛。不过当时私法比较多。总理说的十二表法就是罗马法。<br />
　　温总理：我们说，古代的也好，西方的也好，优秀的部分借鉴过来对研究中国当前的法制建设，很有意义。<br />
　　学生：先秦诸子百家思想的一些基本原理可以作为现代法治的基础。<br />
　　温总理：为什么先秦那段历史在中国文化史上占有很重要地位呢？那时思想比较解放，出了诸子百家，允许百家争鸣。因此出了许多文学家、思想家。我一直在思考，先秦时期社会虽然是很混乱的，但思想是很活跃的。这是一段很重要的历史。<br />
　　学生：我平时也看一些孟子的东西，如他的民本思想。<br />
　　温总理：孟子说：“民为贵，社稷次之，君为轻。”民本思想实际上也是当时的法学思想，也可以说是当时的启蒙思想。<br />
　　学生：总理对我们学校有什么印象？<br />
　　温总理：我刚才说过这是一所非常重要的学校。一个国家如果把依法治国作为基本方略的话，就必须重视法律人才的培养，从这点上说，说它怎么重要都不过分。在世界上，各个国家都很重视法律人才的培养。我们这方面的人才不是多了，而是少了，太缺乏了，所以你们学法律应该感到骄傲。<br />
　　学生：总理对我们学法律的学生有什么希望和要求？<br />
　　温总理：我刚才讲了很多了。你说我对你们有什么要求，其实就两点。第一，要对国家对人民对社会有高度责任感。因为你知道，你立这部法律，能影响整个国家、社会和人民。第二，要有高尚的人格。法的核心就是公正公平，这要靠人公正执法，如果不具备这种思想境界，你就做不到公正和公平。从这点来讲，对学法律的学生不只业务水平要求高，思想品德也要求高。<br />
　　学生：对！法律人必须有这样的素质和道德修养。我们校训也说明了这一点。“厚德，明法，格物，致公”，把德放在第一位。<br />
　　温总理：厚德，明法。其实厚德也可以把它理解成明道。前面我已讲了，就是不仅要明法，还得明道，具有高尚的道德。懂得真理，懂得法律，懂得道德。道德是明法和守法的基础。它更崇高，也更有力量。</div>

							
							
							
							
