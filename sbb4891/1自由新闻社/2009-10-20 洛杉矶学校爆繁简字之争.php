【明报专讯】在有很多华人聚居的美国洛杉矶，不少高中都将中文列为第二语言。随着近年中国大陆移民增加，要求课文改用简体字的声浪也开始出现，令洛杉矶爆发繁简之争。<br />
 <br />
<strong>内地移民上升促改用简体字 </strong><br />
<br />
《洛杉矶时报》报道，在华人聚居地，教授中文已行之有年，过去由于新移民多半来自香港与台湾，繁体中文一向是课程主流。但随着大陆移民渐增，不少原先教授繁体中文的高中，面对愈来愈多家长抗议，希望将课程由繁体改为「较多人使用」的简体字，有部分学校因为不愿更改，出现学生流失的问题。 <br />
<br />
报道引述美国中小学中文协会（Chinese Language Association of Secondary-ElementarySchools）的调查，指出1994年在139所抽样学校中，只有17%教授简体中文，教授繁体中文的达40%，但到了2007年，263所学校中，近半学校教授简体中文，只有11%的学校仍教授繁体中文。 <br />
<br />
3年前开始教授中文的帕洛阿尔托（Palo Alto）市立高中语言课程教务主管马苏达（Norman Masuda）表示，应该让孩子学习能够立即使用的语言与文字，「多数学生会去中国，而不是台湾，你必须跟随潮流」。但来自台港的家长则大力反对。为了让家长满意，部分学校只好简繁体字同时教授，由学生自行选择。 <br />
