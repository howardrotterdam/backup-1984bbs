&nbsp; &nbsp;&nbsp; &nbsp; 郑渊洁宣布退出北京作家协会<br />
<br />
 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;郑渊洁<br />
<br />
 <br />
<br />
&nbsp; &nbsp; 依据《北京作家协会章程》第十条，郑渊洁宣布退出北京作家协会。<br />
<br />
&nbsp; &nbsp; 原因：本人上个世纪八十年代初加入北京作家协会。彼时的北京作家协会和此时的北京作家协会不同。那时的北京作家协会工作人员同作家联系密切，不为自己谋私利，一心为作家服务。给我留下深刻印象的北京作协工作人员有郑云鹭、陈红军、李芸。进入2000年后，北京作家协会管理水平发生了变化，办事机构领导和相关工作人员素质和品质今不如昔，北京作家协会的级别虽然从“正处”升为“ 副局”，但是为作家服务的水平却从“正处”降级为“副股”。2000年后，本人明显感觉受到北京作家协会的排挤。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 事实：2003 年9月，北京作家协会召开第四次会员代表大会。本人当选代表。北京作家协会在网络尚不发达的2003年，竟然选择在网上向我发出开会通知。我既不让你来开会，又通知了你。而且保证你看不到这个通知，但是又有据可查。从这件事上，可以看出北京作协是充满智慧的机构，只是如果用在团结作家振兴文学创作上就好了。根据《北京作协章程》第四条，作协负有联络作家的职责。“开会通知无法送达手中”只能证明作协居心叵测。本人从1990年开始使用邮政7801信箱保持同外界的联系至今，一直没有变更。自1990年起至2000年，北京作协一直使用这个信箱向我邮寄会刊。而在第四次会员代表大会召开之前，我的邮政信箱从未有退回北京作协开会通知的记录。事实上，北京作协是不想让我参加代表大会，在我当选代表后，剥夺了我的参会权利。<br />
<br />
&nbsp; &nbsp; 担忧：按照《北京作协章程》第十二条的规定，第五次代表大会该开了。我替北京作协担忧：万一郑渊洁又当选代表，这次作协再采用网上通知的方法，估计我在几分钟之内即获悉。我以为，这次北京作协大概会选择群发手机垃圾短信的方式通知我开会：想办假证吗？想开假发票吗？想刻假章吗？请拨打以下电话……北京作协第五次代表大会召开在即，目前，由于郑渊洁的联系方式变更，作协的通知无法送达他手中。希望这位代表或者他的亲朋好友看到此垃圾短信之后，尽快与作协联系，以期届时能邀请他参加会议。联系人李……<br />
<br />
&nbsp; &nbsp; 我不想在第五次代表大会召开之前再给北京作家协会添麻烦，我宣布退出北京作家协会。<br />
<br />
&nbsp; &nbsp; 我希望北京作家协会的工作人员把主要精力放在联络作家上，不要把主要精力用于为自己谋私利，花好纳税人的每一分钱（在纳税人还让你花之前）。<br />
