													<h2>一个90后眼中的535</h2>
						
						
																											<div id="postmessage_104849" class="t_msgfont">&quot;陆肆&quot;事件20周年，对90年代出生的一代人有什么意义？当年参与抗议的学生与现在的90后年龄相近。90后对&quot;陆肆&quot;、对国家有怎样的思索呢？ <br />
<br />
BBC中文网记者嵇伟采访了来自四川的高中三年级学生陈三（非真名）。 <br />
<br />
问：你怎么知道20年前的&quot;陆肆&quot;学生运动呢？ <br />
<br />
陈三：去年高二的时候，奥运会期间，我看到海外中文新闻网站上面关于陆肆屠杀的内容。我的同学说，可以用代理（服务器）上那些网站，他教我用，我因此看到了很多平时看不到的，比如在电视上和网络上看不到的内容。 <br />
<br />
问：关于&quot;陆肆&quot;事件你具体知道些什么呢？ <br />
<br />
陈三：我知道是因为胡耀邦去世，然后各大高校的学生举行悼念活动，然后慢慢地演变成一种反贪腐、要民主的运动。他们反对老人政治、反对独裁，然后又加入了一些比如说要媒体自由、要开放报禁等等的诉求。后来随着赵紫阳下台，党内的强硬派在6月3日晚上对他们强行地镇压，死伤了很多人吧。 <br />
 <br />
问：在去年以前你就从来都没有听说过&quot;陆肆&quot;这件事？报纸上、教科书上和父母亲戚的谈话中都从来没有涉及过？ <br />
<br />
陈三：教科书上是没有的，它不可能在教科书上面写，八十年代历史我们一无所知。我们连国家领导人出现过胡耀邦、赵紫阳，都不知道。 <br />
<br />
初中和高中的历史课本上，胡耀邦和赵紫阳从来没有出现过。中英关于香港前途会谈，由赵紫阳代表中国政府签署的协议，在教科书上面都没有提。 <br />
<br />
这个学潮我是知道的。以前我只听说过，不晓得是谁无意中讲起过学潮，就只是两个字，但里面也不知道怎么回事，发生屠杀。我还以为就这样不了了之闹腾吧。我想不到政府会这样做。 <br />
<br />
政府平时在教科书也好、网络也好给我们灌输的就是人民的党代表人民，根本不可能用军队来对付民众或者是学生吧。 <br />
<br />
问：当你第一次知道&quot;陆肆&quot;事件的真相的时候，你的反应是什么？ <br />
<br />
陈三：很愤怒，然后也很难过。愤怒就是对政府的这种行为、这种犯罪感到很愤怒。难过就是面对同胞的惨死心理不是滋味吧。当时第一眼看到这个视频的时候就是这么一个心情吧。 <br />
<br />
我问妈妈，这么多年为什么没提起过呢？我说你知不知道开了枪，很多市民和学生是牺牲了。我妈说知道啊，但是她是听广播，香港电台也说过。我说你怎么不跟我说，她说这有什么好说的。 <br />
<br />
问：你对当年的学生运动有什么看法呢？你支持他们的观点和行动吗？ <br />
<br />
陈三：我当然支持他们的观点和行动。现在大家只关心经济发展，大家都向钱看，可能现在很多人就是各自找路子各自发财，不会想着为国家、为社会、为民族而对抗。 <br />
<br />
我的同学说他们觉得&quot;陆肆&quot;很可笑，简直疯了，就是这么一个观点。但是我觉得他们（当时的学生）都是非常敢于跟强权对抗吧，换成我们现在这个社会、这个年代，这是不可想象的。 <br />
<br />
当然我认为他们应该注意斗争的策略，就是当政府表现出退让的时候就应该撤下来，不过后面刘晓波他们绝食我觉得就不应该了，引发了一种不可挽回的结果。 <br />
<br />
问：你对政府当时的处理方法怎么看呢？ <br />
<br />
陈三：我觉得这是一种犯罪吧，当初的这种做法等于在整个民族心里搁上了一个伤口。 <br />
<br />
问：你的同学或者是同龄的朋友，他们以前也从来不知道陆肆吗？是在去年奥运会媒体开放之后才知道的？ <br />
<br />
陈三：以前我们什么都不知道，我们一无所知，知道了后就很愤怒。然后有一些知道的，他们听自己的爸爸妈妈说的，但是他们也没有提起过，我们提起的时候他们才告诉我们，也算是以前耳闻过，但是不了解事情的详细经过吧。 <br />
<br />
去年我和另外一个隔壁班的同学，他把维基百科，把那个历史的整个事情打印出来，我就给我们班同学看，然后他们才知道详细情形。 <br />
<br />
问：在知道&quot;陆肆&quot;的真相之后，你对官方的说话和媒体的报道是不是仍然和以前一样信任呢？ <br />
<br />
陈三：我当然不信任了，现在我看新闻我都会看央视、看《环球时报》，但是我还会看海外的中文新闻网站。 <br />
<br />
去年有人说BBC西藏的报道不实，说CNN 造假，但是我觉得CNN再怎么样的，它的权威性、它的独立性也比《人民日报》和中央电视台强吧。因为世界上很大部分人都会看CNN、看BBC，而不会去看新华社，看《人民日报》，因为它们的政治立场太偏颇了。 <br />
<br />
比如说地震吧，今年这个时候我会看央视的直播，但是我也会看BBC关于那些豆腐渣工程的报道，不让这些父母们祭奠自己的孩子，或者把他们拘留，这些侵犯人权的我都会看。就是不会再像以前那样相信政府、相信中国内地媒体。 <br />
<br />
还有一些香港媒体我也不信，像凤凰卫视、凤凰网我也不信。我现在综合看，对台湾媒体我还是比较相信的。 <br />
<br />
问：评论认为&quot;陆肆&quot;镇压打破了中国人对共产党的信任，但是现在中国各地大学生入党的人数这几年来不断地增加，你认为这是对共产主义信仰的新一轮崛起吗？ <br />
<br />
陈三：这不是吧。这主要是跟自己的前途、自己的事业、生活有关吧。因为入党会有很多好处，比如说你在单位，你入党你就可以晋升、当干部，你不入党的话就不可能。这是共产党制度下的一种趋势，而不是什么兴起的高潮。 <br />
<br />
89年屠杀过后，应该说国人就此对共产主义这个信仰已经产生了动摇。 <br />
<br />
比如说我们入共青团吧，就是抱着一种别人都入你不入，你会觉得自己吃亏的这样一种心态。 <br />
<br />
问：你认为隐瞒了二十年的&quot;陆肆&quot;真相现在是不是有必要对公众，尤其是对不知情的年轻的一代公开呢？ <br />
<br />
陈三：当然，我认为这应该公开，它这样的封锁掩盖事情真相，不利于和解，只会加深仇恨，我认为当然应该了。这样牺牲的同胞，他们的在天之灵才会得到安息。比如说丁子霖领导的天安门母亲，（公开真相）才会抚慰一下他们的心灵。 <br />
<br />
问：中国大陆很多历史事件，不光是&quot;陆肆&quot;，也是对年轻一代不公开的，所以年轻一代都不知情。除了&quot;陆肆&quot;之外，你对文革、反右这些政治运动了解吗？ <br />
<br />
陈三：了解的不多，以前我只是在历史教科书上了解文化大革命，后来我经过妈妈知道有大饥荒、三反、五反、反右，还有内蒙古的内人党事件，还有云南壮族的沙甸事件。就是从去年奥运会开放媒体之后，还有用代理上网，后来我就慢慢地了解这些事情了。 <br />
<br />
问：你怎样看中国的前途？ <br />
<br />
陈三：我认为，现在中国经济持续发展这么多年，这是一种牺牲个人的权利来进行的。但是这种专制到了一定程度都会爆发，比如说遇到经济危机或者政治危机的时候，当前这种稳定的局面就会产生动摇，最终的民主化是一个大的趋势。 <br />
<br />
你想想看，现在世界上专制的国家就只有那么几个吧，民主化是历史发展的必然。比如说香港政府拖延香港的民主进程，就想慢慢等中国大陆吧，到经济发展慢慢跟上香港，比如说上海、深圳、北京，他们会说&quot;你看香港都可以实行民主，为什么我们就不行呢？&quot;就这样子，慢慢的中国大陆也会民主化。 <br />
<br />
