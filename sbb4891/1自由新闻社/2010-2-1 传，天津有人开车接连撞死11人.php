不过，“张义民被单位领导逼得忍无可忍”的说法，获得了部分网友的相信。大部分网友认为，张义民必须偿命，但也有人认为张可能患有精神疾患。对此，也只能等待警方进一步的调查和披露。<br />
<br />
有媒体转引一名不愿透露姓名的警察说法，称张义民刺完李涛后，以为对方可能已死，从而夺车而逃。杀人偿命的想法，导致他孤注一掷。<br />
<br />
质疑<br />
<br />
昨日中午，在天津市滨海新区区委宣传部内，多家媒体的记者提交问题，请求官方公布2月1日上午警方追捕张义民的处置程序和事件过程。事发后，有人对警方的做法和信息发布的方法提出了质疑。<br />
<br />
2个多小时控制疑犯？<br />
<br />
警方尚未公布处置预案<br />
<br />
当地政府2月1日提供的宣传通稿中，张义民捅伤李涛，惊动其他同事，并劫车冲出天保运业公司的时间，是在“1日8时许”，通稿没有交待张义民被警察控制的具体时间。<br />
<br />
多家媒体的调查证实，张义民在北海路于第九大街路口被警方控制的时间，已过当天10时。即，张义民驾车在保税区及开发区的街道上行驶了2个多小时，致死9人，伤11人后，才被警方逼停。<br />
<br />
接到报警后的2个多小时内，警方如何制定处置方案，又如何执行，由哪一级领导指挥？面对疑问，警方未作回应。<br />
<br />
为什么要下令追车？<br />
<br />
反恐巡警称追车之外办法很多<br />
<br />
在张义民被控制前，数十辆警车曾参与了对其驾驶大客车的追截，共致3辆警车被撞，4名警员受伤。<br />
<br />
目前至少有两名市民。据伤者蒋学增家人讲述，蒋当时驾驶一辆小卡，载着外甥马东正常行驶时，追击张义民的一辆警车从后边冲上来闯红灯转弯，撞上了小卡的驾驶室，导致蒋学增生命垂危，马东也严重受伤。<br />
<br />
蒋家人质疑称，警方拦阻犯罪嫌疑人是职责所在，但“能不能讲点方式，至少顾及到无辜者的安全？”<br />
<br />
而从张义民的公司，到冲撞最惨烈的第十一大街，至少有一半路程中，并没有张义民冲撞行人的目击和报道。因此，有人质疑，是否警方后续而来的追截，导致了张义民慌不择路，乃至大开杀戒？<br />
<br />
某省一名参加过多次反恐演习的巡警称，警方对付张义民这样的“马路杀手”，有很多办法，比如快速设置路障，撒铁蒺藜，铺设阻车钉带等。<br />
<br />
该巡警介绍，一般来说，在道路摄像头的配合下，只要训练有素，出警迅速，像这样的追击场面，并不会出现。<br />
<br />
对上述疑问，天津警方迄今没有回应。因此，有家属要求警方公布事发路段监控录像，并解释下达追车命令是否必要。<br />
<br />
信息公开为何如此难？<br />
<br />
小道消息满天飞 宣传部门称再等等<br />
<br />
案发后，当地政府除了公布一份通稿外，没有再透露任何案件及善后的进展事项。<br />
<br />
对种种疑问，滨海新区宣传部一再告诉媒体记者“再等等”。<br />
<br />
而同时，当地民众和网络上，流传着不少此案的小道消息。有人质疑政府公布的死伤人数，有自称是目击者，大谈血腥场景，还有人为张义民喊冤，称其发狂杀人，实为被逼。<br />
<br />
对上述传言，滨海新区宣传部负责人称并不属实，但没有进一步解释，提供权威信息，予以辟谣。<br />
<br />
<br />
<br />
无论收治伤者的医院，还是案发公司天保运业，工作人员称接到指令，谁接受记者采访，就将受到处分。<br />
<br />
