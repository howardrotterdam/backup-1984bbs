“突变”恐慌症<br />
用“焦虑”一词形容此时各类厂家的心情最恰当不过。突然的政策变化，让行业内各方从<br />
业者都感到心里没底，不知道自己下一步该如何安排工作。突然宣布改变芯片标准，打乱<br />
了许多厂商的生产计划和市场安排。<br />
“本来以为中星九号的上天能给这个行业带来一丝宽松的发展环境，没想到这说变就变了<br />
。要关掉一半以上的节目，老百姓肯定会有意见，代理商的很多产品现在是卖不出去了，<br />
再就是可能面临退货的问题，但我们不会开这个口子。这样的突变可以理解为不可抗力，<br />
是政府政策的重大变化，企业对于消费者没法承担责任。”一位厂家代表说。<br />
许多厂家忧虑的问题是：到底何时才能有个明确的时间表？什么时候开始加密节目？到底<br />
关闭哪些节目？现在的接收机从什么时候起开始全面退出市场？新的标准何时出台？招标<br />
何时才能重启？这些是厂家最为担心的事情。因为心里没底，许多厂家的工作都陷入了停<br />
滞，都不知道该做什么？怎么做是好？这令厂家非常忐忑不安。<br />
一位厂家代表直言：反正现在改变标准、加密节目已成定局，我们能期盼的就是领导层赶<br />
紧出台具体时间表，具体执行细则，我们好安排自己的计划和应对策略。现在我们能做的<br />
就是赶紧处理库存，转移损失风险。<br />
某厂家代表向记者介绍了此次“突变”可能引起的连锁反应：这么大规模的突然变化，极<br />
可能引起卫星电视用户、经销商的反弹。那么多的接收机已经进入市场，现在忽然要加密<br />
节目，本来质量不高、节目不多的地方台，关停后将只剩下几个收视率一直就不高的地方<br />
电台节目。10月1号之后就要关停一半的节目，许多用卫星接收锅的老百姓肯定会不满这<br />
样的做法。但长期以来私人安装卫星接收天线就是政策所不允许的。也许这不满更多的只<br />
能是隐忍不发。为了运营，加密节目并没什么错误。但现在这样的思路也许适合于城市的<br />
卫星电视运营，这样也同时兼顾了有线电视的运营，这样的节目设置不会对有线电视形成<br />
太为显著的挑战。但是在农村地区，多少年来，用户安装卫星接收天线，免费收看那么多<br />
的节目已成既成事实。现在一下子少了那么多节目，而且还要收费，人家刚买的接收机本<br />
来是可以看50多套节目，忽然关闭至20多个节目，许多人肯定接受不了，大规模的退货潮<br />
极有可能涌现。<br />
一位厂家代表的措辞更为激烈，他认为：这一次的大翻盘完全是某些人一时脑热就决定了<br />
一个行业的走向的，而且是颠覆性的改变。不考虑现实问题是不对的，现在用的接收机多<br />
数在农村市场，这么大的翻盘，有多少农民需要换机子？每个农民一家一年能挣几个钱？<br />
接收机的价格对于他们来说还是一笔不小的支出。都彻底换掉已有的接收机，用户是否会<br />
接收？不接受怎么办？总不能不让他们再回到看不到电视的时代吧？<br />
愿明天更好<br />
一位厂家代表表达了这样的诉求：现在我们最大的期盼就是希望能采取4+4的模式，保留<br />
原有的4套转发器，再另加4个转发器，原有转发器上的节目仍然免费，新加转发器的节目<br />
加密收费。这样就皆大欢喜了：原有的接收机用户、代理商、生产商就不会面临现在这么<br />
多问题。原有的设备都可以用，节目也不减少。而且同时能保证卫星电视的市场化运作。<br />
<br />
一位业内消息灵通人士告诉记者：现在节目加密的安排已成定局，不可能有转机了。而且<br />
新的芯片标准方案已于日前上报。从国庆之后开始替换原有的机顶盒，到 2010年要全部<br />
换完。但新标准的启用需要工信部的审批，新产品开发需要质检总局的审核，没有3个月<br />
的时间是搞不定的。3C认证肯定是要做的，没那么简单的，这不是一个部门决定的事，需<br />
要其他部门，尤其是工信部的合作。<br />
总局计划是9月30号就开始关停现在的部分节目。10月份开始发标书，开始招标。但是我<br />
们怀疑该进度的可行性，哪有那么简单。这需要3C认证、入网认证、 CA认证、生产许可<br />
