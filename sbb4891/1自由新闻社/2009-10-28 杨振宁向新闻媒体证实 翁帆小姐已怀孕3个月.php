2009年10月30日 08:27:14 　来源：扬子晚报<br />
【<br />
<br />
&nbsp; &nbsp; 就像每隔一两年就要传出“余秋雨和马兰离婚了”、“王菲要复出了”之类的消息一样，“翁帆怀孕”也是媒体热衷于猜测并传播的“新闻”。昨日，不少网站贴出了一则《杨振宁向新闻媒体证实，翁帆小姐已经怀孕3个月了》的“新闻”。本报记者昨天致电身在香港的翁帆，已关机。记者又连线香港的翁帆闺密及钢琴老师之一、“钢琴琵琶公主”孙颖，她通过本报独家澄清：翁帆没怀孕，并通过本报发布了翁帆的独家近照――<br />
<br />
&nbsp; &nbsp; 假新闻做得“惟妙惟肖”<br />
<br />
&nbsp; &nbsp; 在这篇《杨振宁向新闻媒体证实，翁帆小姐已经怀孕3个月了》的帖子中提到，“当时面对镜头，杨先生高兴之情洋溢于表,头颅骄傲高昂。而翁小姐也在旁边娇羞证实,孩子的确是杨先生的。经多方打听，翁帆小姐确已有3个月的身孕，并正在家中静养保胎”。描述虽“惟妙惟肖”却漏洞百出，不过此“新闻”却引起网友铺天盖地的热议。因不少来源都注明“人民网海南视窗”，记者了解到人民网海南视窗“转的中国日报网的稿子”，而中国日报网的工作人员则表示，这个稿子是早上挂的，不过已经删除了，因为该稿来源不详，并不是自己的采访稿。但昨天傍晚记者发现很多网站仍将此新闻挂在网上。<br />
<br />
&nbsp; &nbsp; 美国记者竟打电话恭喜杨振宁<br />
<br />
&nbsp; &nbsp; 同时，从昨天起已不断接到记者骚扰电话的翁帆好友――“钢琴琵琶公主”孙颖通过本报独家澄清此事，“这新闻也太无聊了！这两天我电话都被打爆了！香港等各地记者都来问此事，我都不敢接。昨天居然还有美国记者打电话到杨振宁翁帆家里，开口就‘恭喜杨教授’，让他们夫妻哭笑不得。”说起二人的生活，她透露：“翁帆状态非常好，前几天见到她和杨先生，他们俩刚从沙特阿拉伯回到香港，两人都显得神采飞扬。翁帆现在除了学钢琴之外，还在努力多学习一种语言。”记者问孙颖如何看待两人婚姻，孙颖又说：“其实比起很多年龄合适却道貌岸然地在人前装恩爱、背后就各有各世界的‘假面夫妻’，我由衷感受到杨翁的幸福是发自内心的。他们新婚时就非常好，相信会越来越好，我亲眼看到过很多两人相处的生活细节都很令人感动。翁帆若是真有好消息一定会和好朋友分享的，请大家还是多关注自己的感情生活吧。”<br />
<br />
&nbsp; &nbsp; “照网上说的，她都生好几个了”<br />
<br />
&nbsp; &nbsp; 作为闺中密友，孙颖很替翁帆气愤，不过她说杨振宁平时除了看书从来不上网，也不看八卦报纸，这些造谣对杨老没有影响，翁帆也一笑置之，“我刚跟翁帆通电话，她笑着说这新闻已经是旧闻了，他们从来不会理的。”只是端坐在家中，就无端端不断接到从世界各地打来的恭喜电话，他们感觉搞笑而已，孙颖还调侃道：“照网上说的，她都生了好几个了。”孙颖还说起另外一件“趣事”，“有一次突然有消息大说特说翁帆的爸爸和杨教授的孙女拍拖，说得有板有眼，把杨振宁和翁帆笑了半天，因为杨教授最大的孙女才十来岁，那次也是好多人打电话来问我，”最后她感慨道，“难道这世界没有更重要的事做吗？难道人人都是宋祖德吗？”（记者 张艳）</div>

							
							
							
							
