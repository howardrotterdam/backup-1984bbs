																											<div id="postmessage_520203" class="t_msgfont"><a href="http://news.ifeng.com/mainland/201003/0317_17_1578290.shtml" target="_blank">http://news.ifeng.com/mainland/201003/0317_17_1578290.shtml</a><br />
<br />
2010年03月17日 <br />
<br />
东莞桑拿服务行业专项整治会议召开 卫生局给桑拿老板敲警钟<br />
<br />
本报讯 （记者汪万里）昨天下午3时，东莞市卫生检验中心二楼会堂，东莞桑拿整治大会举行，近200名桑拿老板从各个镇区赶来。<br />
<br />
会上，东莞市卫生局相关负责人一再强调整治桑拿的“压力”，一再明确告知4月26日起进入全面清理阶段；“法律法规明确规定不能做的绝对不能做，尤其是发送手机短信和派发涉黄材料”。<br />
<br />
开桑拿规定更加细化<br />
<br />
昨天，相关部门召集桑拿老板开会，向下传达这样的信息：此次桑拿整治，不同以往。<br />
<br />
去年10月，《东莞市桑拿服务行业管理办法》正式出台，不再对桑拿场所限制总量。<br />
<br />
“虽然总量控制放开了，但我们仍然要求镇街进行合理规划和控制。”市卫生部门提醒在场的桑拿场所老板，“同意发放许可，不等于允许涉黄。”<br />
<br />
此外，卫生局还对桑拿场所设置进行了细化规定，桑拿场所不许设置可调灯光，必须有50平方米以上的休息厅，桑拿房间必须有面积不少于0.25平方米的透视门窗，门窗下沿应距地面1.2米高。桑拿场所不得在政府机关和学校周边200米范围内设立。<br />
<br />
这次桑拿整治动真格<br />
<br />
会上宣布，市政府目前已经下发了《东莞市桑拿服务行业专项整治实施方案》，4月26日开始，将开始全面清理证照经营、超范围经营、未按《东莞市桑拿服务行业管理办法》要求进行登记备案和涉及“黄、赌、毒”等治安问题的桑拿经营单位。<br />
<br />
市卫生部门负责人在会上介绍，前期已经对东莞桑拿场所进行了摸底调查，现在是疏导教育阶段。“今年市政府下定决心要禁赌打黄。”<br />
<br />
招嫖短信绝不能再发<br />
<br />
昨日，东莞市卫生检验中心二楼会堂里坐得满满当当，在门口负责签到的东莞市卫生监督所工作人员告诉记者，来了大概有197家，近200名桑拿老板参会。<br />
<br />
记者在最后面坐下，坐在记者旁边的男子来自东城，他以为记者也是桑拿老板，“你们的场子现在还开吗？我们的停了几天了”。记者告诉他，“我的场子还在开啊。”他笑嘻嘻地盯着记者看。<br />
<br />
主席台上，卫监所负责人已讲到最后，投影上显示，“经营者不能做什么。法律法规明确规定不能做的，绝对不能碰；尤其是发送手机招嫖短信和派发涉黄宣传材料”。主讲人称，“这个招嫖短信惹了很大的祸，一定要注意”。<br />
<br />
但记者在会场不时听到有人拿着电话走到走廊上。“不好意思，我现在在东莞开会，晚点就回去，大哥你们几位啊，没事，你们直接过去就好，我给你们订房。”一男子说。<br />
