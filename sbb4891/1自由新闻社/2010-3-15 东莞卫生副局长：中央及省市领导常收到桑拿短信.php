													<h2>东莞卫生副局长：中央及省市领导常收到桑拿短信</h2>
						
						
																											<div id="postmessage_516957" class="t_msgfont"><a href="http://news.163.com/10/0315/09/61QCEQVK0001124J.html" target="_blank">http://news.163.com/10/0315/09/61QCEQVK0001124J.html</a><br />
<br />
南方日报3月15日报道&nbsp;&nbsp;3月12日,在东莞市卫生监督工作会议上,市卫生局副局长金行中说:“桑拿短信经常发到中央领导、省市领导的手机中。”<br />
<br />
自从去年末,桑拿服务行业由卫生局主管以来,金行中自称成了“第一责任人”。他在会上说:“这个事情管不好,我要被追究责任,你们也跑不了”。<br />
<br />
目前,由多部门联合开展的桑拿服务行业专项整治行动已完成了摸底,即日起开始全面清理,无证、涉黄桑拿场所均在查处之列。<br />
<br />
去年以来,东莞已开展了多次“扫黄”行动,桑拿服务行业在“扫黄”行动中频受影响。然而,市民仍然时不时能收到桑拿短信,路边有时也能遇见有人派发桑拿传单,说明涉黄桑拿业仍一息尚存。<br />
<br />
金行中在全市卫生监督工作会议上,谈及对于管理桑拿服务行业的感受。<br />
<br />
他说:“那些桑拿短信,经常发到中央领导、省市领导的手机中。再这样下去,我要被追究责任,你们也跑不了。”他要求全市卫生监督工作人员加强执行力,“不能再上有政策,下有对策”。<br />
<br />
尽管东莞的桑拿可谓遍地开花,但市卫生局卫监科科长李小伟说,全市有证的桑拿场所只有130多家,“无证桑拿是有证桑拿的数倍”。<br />
<br />
过去一年,全市共查处无证经营的公共浴室和桑拿场所40家,超范围经营场所44家,但至今仍存在无证、涉黄桑拿场所公开经营的现象。<br />
<br />
李小伟说,近期多部门将联合开展桑拿专项整治行动。目前已完成了对桑拿服务行业的摸底工作,马上要进行全面清理整顿。<br />
<br />
此次整治的对象是全市所有桑拿经营单位,重点为无证照经营、超范围经营、未按《东莞市桑拿服务行业管理办法》要求进行登记备案和涉及“黄、赌、毒”等治安问题的桑拿经营单位。</div>

							
							
							
							
