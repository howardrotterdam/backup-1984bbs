<br />
　　这篇题为《中情局与伊朗试验场》的报道说，为了达到目的，中情局这次依靠的是一种全新的武器：控制移动电话。<br />
<br />
　　报道认为，英美和以色列情报部门已经酝酿出一套以广泛利用手机为基础的心理战战术，并用于伊朗，通过散布耸人听闻的消息引发民众的强烈不满情绪。首先，在选举当晚通过短信散布消息，指出宪法监护委员会已经通知穆萨维他获胜的消息。于是几个小时以后，当艾哈迈迪―内贾德获胜的官方消息公布之后，看上去就像一个大骗局。但３天以前，连穆萨维也认为，艾哈迈迪―内贾德会大获全胜。美国民调部门也预计，艾哈迈迪―内贾德的得票率会高出穆萨维２０个百分点。<br />
<br />
　　报道说，随后一些社交网站和微型博客的用户也开始通过手机短信接收到一些关于政治危机和街头抗议行动的似真似假的信息。这些匿名信息的内容大多是关于枪击和大量人员死亡的，但这些消息直到现在也未得到确认。<br />
<br />
　　与此同时，中情局还指使美英等国的反伊朗分子继续煽动混乱局面。这些措施使人们无法区分微型博客网站Twitter上信息的真实性，谁也不知道这些信息是德黑兰抗议活动的目击者还是美国中情局特工发布的，其目的就是制造更大的混乱，让伊朗人内讧。<br />
<br />
　　本报伊斯兰堡6月23日电　（记者孟祥麟）据伊朗英语新闻电视台23日报道，伊朗宪法监护委员会（宪监会）当天表示不会推翻6月12日的总统选举结果。伊朗伊斯兰共和国通讯社23日报道称，伊朗议会已将新任总统就职日期定在7月26日至8月19日之间。<br />
<br />
　　宪监会发言人卡德霍达伊22日晚表示，没有发现此次总统选举中的重大欺诈行为。因此，选举结果不会作废。<br />
<br />
　　本报华盛顿6月22日电　（记者温宪）美国国务院发言人凯利22日表示，继续与伊朗进行对话和接触，是美国已经做出的“战略决定”。<br />
<br />
　　在美国国务院每日例行新闻发布会上，有记者问，在伊朗目前形势下，美国是否准备取消对伊朗官员出席美国独立日庆祝活动的邀请？对此，凯利明确表示，美国不打算取消邀请。此前，为了体现与伊朗进行“接触”的政策，美国曾宣布将邀请伊朗政府官员出席美国7月4日独立日庆祝活动。</div>

							
							
							
							
