													<h2>四年巨变――伊朗动荡探源</h2>
						
						
																											<div id="postmessage_133939" class="t_msgfont">from:卫报<br />
<a href="http://guardian.yeeyan.com/guardian/46814" target="_blank">http://guardian.yeeyan.com/guardian/46814</a><br />
<br />
<i>四年前，年轻人不问政治享受生活；今天，数以万计走上街头。究竟是什么促成了这种巨变？ </i><br />
<br />
Azadeh Moaveni<br />
guardian.co.uk&nbsp; &nbsp;2009年06月19日 <br />
翻译 <a href="http://www.yeeyan.com/space/show/37468" target="_blank">Juliano.tank</a> @ <a href="http://www.yeeyan.com" target="_blank">yeeyan</a>&nbsp; &nbsp;&nbsp;&nbsp;<a href="http://www.guardian.co.uk/world/2009/jun/19/iran-young-people-anger-unrest" target="_blank">原文链接</a> <br />
<br />
<img src="http://static.guim.co.uk/sys-images/Guardian/About/General/2009/6/18/1245347661456/Unrest-Iranian-Presidenti-001.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<font color="#ABABAB">2009年6月17日，伊朗，德黑兰：伊朗总统选举中被击败的改革派候选人米尔・侯赛因・穆萨维的支持者正在示威。（供图: Getty Images/Getty Images）</font><br />
<br />
 <br />
<br />
笔者曾全程报道2005年夏天伊朗举行的总统大选，在那次选举中，穆罕默德・艾哈迈德・内贾德令人震惊地首次获胜。当年我的任务不是报道整个选举过程，重点是在于介绍当地青年对于政治的态度。我花了三周时间与学生领袖、伊斯兰武装力量动员队（一个全部成员均为志愿者的准合法民兵组织）成员、疲惫的侍者以及都会时尚人士交谈。大多数人或是忙着找一份兼职工作，或是已经因现有的数个兼职工作而精疲力尽，总之他们对政治不太关注。而那些不必面对经济问题的年轻人则穿梭于画廊中，或在石榴果汁店购买迷幻药。<br />
<br />
从整体上来看，笔者发现伊朗青年无论是虔诚的还是世俗的，显然都游离于政治之外。他们似乎更关注本身的当前自由，而不大愿意为政治变化冒任何风险。<br />
<br />
然而四年过后，伊朗青年抛弃了对政治的冷淡，相反，他们数以千万计地走上街头，充满激情地抗议一场他们认为存在欺诈行为的大选。这些在2005年大选中甚至无心投票的年轻人现在成了严肃而勇敢的抗议者，以支持在他们心目中理应胜选的米尔・侯赛因・穆萨维；Facebook被屏蔽与这个国家推崇的殉道精神一起引发了更强烈的愤怒和抗议。<br />
<br />
那么，伊朗的如此巨变究竟是因何产生的？内贾德担任总统时期的作为或许是原因之一。大多数伊朗人都感到现在的生活比四年前更加痛苦，这与他们的阶层、年龄和种族背景无关。现在伊朗各地抗议活动的剧烈程度同时暗示着一种绝望，这种根深蒂固的绝望，远不是对一场看来被盗取了成果的选举感到愤怒那么简单。笔者认为，这也反映了伊朗人对伊斯兰革命政体在根本上的反感，因为在他们心目中，这个政体已经变得不负责任、无视法律、贪污腐化，无法满足其人民最基本的需求。<br />
<br />
2005年夏天，笔者作为一名记者迁至伊朗并建立家庭。我发现这里的生活虽然比较困难，但还是可以忍受的：经济形势虽然较差，但却有飞涨油价支撑；交通和污染都令人窒息，但至少你可以在街上搀着异性朋友的手，或者与异性朋友一起喝咖啡而不必担心会遭逮捕。书店里堆放着最新的西方畅销书，杂志和文学期刊繁荣昌盛。当局偶尔允许举行摇滚音乐会，并且容忍年轻妇女对伊斯兰着装法规的蔑视。就连一向严肃的阿亚图拉肖像壁画也被重新描画，带上了亲切的笑容。伊朗人仍然有着更高的追求，但如果有人问起他们的生活跟以前相比怎么样，大多数人都会回答比以前要好。<br />
<br />
内贾德原教旨主义者的形象曾引起疑虑，人们担心他会以饱满的精力强制施行社会方面的种种限制。但镇压从未真的发生。内贾德宣称，比起妇女的面纱和着装，伊朗还有更重要的事情需要关注；于是，4800万左右30岁以下伊朗人认为，他们业已视为理所当然的宽容将可以延续下去。<br />
<br />
2005年末，内贾德在伊朗核计划问题上对西方采取的态度逐渐强硬起来，以扩大他在国内的政治影响。他把西方描绘成一个敌人，说其阻止伊朗发展科技，以此恐吓和削弱伊朗。此时恰逢乔治・布什总统牛仔式的夸夸其谈惹怒了不少伊朗人，这些人的情绪为内贾德的民族主义言论所激发，变得越来越自负。到了2006年春天，内贾德已经成功转型为一个国家英雄。即使是西化的、世俗的伊朗人也将他的口号“核技术是我们的绝对权利”挂在嘴边。那年春天，笔者参加的每一次宴会上都少不了有人在兴致勃勃地谈论他们的不凡总统。当他撤销一条禁止妇女观看足球比赛的禁令时，我的一个亲戚甚至拿他同伊朗20世纪的立国者伟大的礼萨・汗相提并论。<br />
<br />
大多数伊朗人沉浸于总统核言论所激发的民族主义情绪中，他们确信伊朗在世界上的威望与影响力正在恢复，但接下来发生的事情却大大出乎他们的意料。问题首先出现在经济方面，由于内贾德采取扩张性财政政策，通货膨胀率达到了20%，后来甚至更高。尽管我的工资是以美元支付的，可我在购买大量水果时也会感到价格过于昂贵；我的保姆家里开始有规律地每隔几天就吃一天的素，而我的一个朋友则不再购买能辅助治疗孩子疝气的进口婴儿奶粉。德黑兰的房产价格上涨了200%，数千对准备结婚的夫妇发现他们攒钱的速度远远赶不上房价的上涨。<br />
<br />
经济的衰退使得人们转而开始反对总统，但真正决定他命运的，是他恢复了伊朗人视为古代史的严厉社会控制。在2007年夏天，当局四处出击，没收了德黑兰市内的所有非法卫星接收天线。一天清晨，一队警察突然袭击我们居住的楼房，一脚把设备从楼顶上踢了下来。我六岁的侄子为失去卡通频道而开始哭泣，泪眼朦胧的他抬起头来，迷惑地注视着警察没收邻居的手机，因为这位邻居正在用手机拍摄装满了天线的警用卡车。<br />
<br />
这个夏天稍晚的时候，当局开展了一场全方位的恐吓活动，目标是那些被控着装不符伊斯兰风格的年轻人。在几周之内，警方就拘留了15万人，我认识的所有女性都出门去购买肥大的长袍，那可是我们在上个世纪90年代末期的着装。虽然这场着装风暴也将年轻男性列为目标，但女性却遭到当局特别残酷的对待。政府对女性的轻蔑与日俱增。它全然不顾伊朗人对西方制裁的恐惧，却把注意力放在一条可能助长一夫多妻制的法案上。在那之后不久，政府宣布了一个据称可以解决伊朗婚姻危机的计划。这个叫做“半独立婚姻”的计划，实际上不过是原有制度的一个空洞外壳，它确保男性具有合乎法律和教规的性权利，却剥夺了传统婚姻中女性拥有的安全和社会地位。在互联网新闻网站和报纸上，这个计划遭到女性的猛烈抨击。我的一个女性朋友是教英语的，原本男女同学可以一起听课，但后来两性学生却不得不分开。她抱怨政府是在拿七世纪的规则来对付现代女性。<br />
<br />
更让伊朗人泄气的是政府当年夏天出台的汽油配给计划，以及因此而产生的长长人龙。在晚上我们可能要等待数个小时才能给汽车加上油，后来对新计划感到愤怒的抗议者一把火烧光了我们住宅附近的加油站，我们从此不再使用汽车。伊朗的街道让我想起了战后的巴格达。出版审查制度愈加严厉，第七版的社会科学教科书因为没有通过审查而无法付印。道德警察显眼的白色警车在街上巡逻，让年轻人陷入无尽的焦虑之中无法自拔。<br />
<br />
一天早上，我带着我的小孩到山边散步，这时一个十几岁的女警抓住我的胳膊，试图将我带到一辆警车上去。“你的袖子太短了，”她吼道，并且对附近其他人脸上的厌恶表情熟视无睹。就连我们那里的特产销售商，一个极其虔诚的人（他的妻子非常温柔，身上穿的是那种覆盖整个头和身体的长袍）也无法掩饰他对内贾德的愤怒。“他毁了这个国家，”他绕着堆起来的番茄和无花果飞快地踱着步子，“为什么没有人阻止他？”<br />
<br />
我和我的家人2007年就离开了伊朗，后来我又去过那里几次，我发现人民的情绪正在向更深的绝望滑落。原本决心在伊朗度过一生的年轻亲属们如今在讨论移民。年龄稍长的朋友们在穆罕默德・哈塔米总统的八年任期中享受过温和的政策，精神负担大为减轻，但现在他们脸上刻满了愁苦。今年早些时候我在伊朗停留了一个月，在我看来，人们对六月的大选怀有矛盾的情绪，但无论如何，内贾德必须下台是他们共同的决心。我的一位女性朋友失去了教学的工作，原因是没有通过一个叫“你能正确祈祷吗？”的测验。一个亲属被要求参加部门的集体祈祷会，否则就会遭到解雇。人们早已习惯的有限自由、视为自然的开放风气现在已被夺走，但我认识的人大多数都对此感到不能理解。<br />
<br />
即使是在内贾德上台之前，伊朗也面临着世界上最为严重的人才流失状况。每一年，这个国家最聪明、最有才华的年轻人离开祖国去西方工作，去建设别国的技术、医疗、航天事业。他们离开伊朗是因为在这里找不到与其教育程度相符的工作，而且大多数人认为伊朗的繁荣由阿亚图拉及其后裔们独享。即使是那些个人条件达不到移民要求的年轻人也认为伊朗是一个缺乏机会的国度，如果有机会他们宁愿在西方国家挣扎求存也不愿留在伊朗。<br />
<br />
这种态势的出现远比内贾德的总统任期更早，三十多年以来，它一直在撕扯着伊朗的众多家庭。如果说一周以来的抗议活动中人们看到的形形色色脸庞强调了什么问题的话，那就是这种扎根于伊朗人内心深处的悲哀，他们的愤怒不是因这次选举而产生，而只是被选举的结果所扰动。今天，伊朗人正在表达他们对伊斯兰共和政府制度整体的不满。他们并不一定是要推翻这个政体，只是在表达他们对其无能失望到了何种程度。<br />
<br />
这个星期我看到了许多来自伊朗的照片，但其中最令我震撼的照片，是那些愤怒的妇女攻击警察并领导抗议人群。在过去十年里，富有经验并且受过良好教育的伊朗妇女试图以平和的方式改善现有的社会系统。接受过大学教育的伊朗人有60%是女性，她们离开学校时拥有着对职业生涯的期望和对自己在家庭以及社会地位的现代态度。她们耐心地请求国家基于法律给予她们更公平的权利。但每一次她们遭受的都是轻蔑。她们在抗议活动中的活跃表现正是要告知政府，她们不会再继续忍受对她们的歧视和轻蔑。<br />
<br />
记得在2001年，我参加了在德黑兰西部举行的一场足球聚会，当时那里有很多年轻人徘徊街头举行政治抗议。防暴警察迅速从阴影中冲出来，用警棍殴打抗议者。我的一个女性朋友和我自己都遭到殴打，我们两个走在躁动的城市街道上互相比较身上的瘀伤，并且讨论报纸上会不会提到这件事。第二天我们发现各报噤若寒蝉，于是我将身上的伤痕拍成照片，用电子邮件发给我的朋友们。<br />
<br />
如今年轻的伊朗人正使用互联网，将抗议示威的新消息传播出去。世界各地似乎对他们掌握互联网的速度感到惊讶，但事实上，他们已经安静地实践了整整十年。<br />
<br />
<br />
&#8226;阿扎浴つ芬妮从1999年起为时代杂志和其他出版物报道伊朗。她与希尔琳・艾芭迪共同创作了《伊朗觉醒》，个人的著作有《口红圣战》以及最新出版的《德黑兰蜜月》。</div>

							
							
							
							
