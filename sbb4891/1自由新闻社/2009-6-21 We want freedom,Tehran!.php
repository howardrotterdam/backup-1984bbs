2008-03-22 22:39:43 　　来自: 纹丝不动 (以不动应万动。) <br />
Persepolis / 我在伊朗长大的评论&nbsp; &nbsp; <br />
<br />
　　大约九百多年前，伊朗诗人海亚姆曾在波斯湾，面朝大海咆哮： <br />
　　我们从何而来，又将前往何方？ <br />
　　 <br />
　　归宿，似乎是所有古老民族宿命般的寻找。因为我们曾在历史里留下了精美的足迹，于是让后来人，不由得感叹那些遗失的帝国和财宝，那些传统价值观在现代社会里的盛开与萧条。 <br />
　　中国与伊朗，在漫漫的岁月河流中曾安静而相似的成长，我们的历史上有着同样辉煌繁盛的古代帝国，那些戴着铁制面具的汗血马曾经横跨欧亚，那些明晃晃的阿拉伯弯刀溅起的鲜血亲吻着异族士兵的脸颊；同时我们也有着被西方列强殖民的屈辱近代，三桅的战船和洋人的火枪激起的强大反抗至今仍在这个民族的血液里热烈的流淌。 <br />
　　 <br />
　　东方有红色的中国，而黑与白，就构成了现代的伊朗。黑色的石油，是工业的血脉，白色的伊斯兰教，是整个国家的信仰。黑色的冰冷，白色的单纯，就一如这部黑白的动画片《茉莉人生》，其中作者玛赞・莎塔碧曲折丰富的人生经历，关于自由和生活的独立思考，在明暗交替间，散发着阵阵清新袭人的茉莉花香。 <br />
　　 <br />
　　出生在王室后裔家庭的主人公，伊朗小姑娘玛赞几乎是伴随着动荡长大。童年的她早熟，有点野。她喜欢李小龙，喜欢Rock&amp;Roll，然而在那个国家，革命和战争，一直充满了她童年的生活。这也让她对善恶和人性有着敏感而直观的分辨――她目睹了战争的毁灭与摧残，他遭受了亲人的离别。影片中除了玛赞的家人，伊朗，在她的阿拉伯风格的回忆画面中，几乎是一片苍白，没有一丝美好。 <br />
　　 <br />
　　从法国大革命开始，大多数的社会变革总会伴随着铁血，自由引导人民，却也引诱了那些政治投机者。他们扮演着为民族牺牲的大无畏角色，成功之后，却转眼就建立了理想主义甚至个人主义下的专制和压迫。伊斯兰革命后的那个共和国，世俗化被取缔，他们强迫妇女重新戴上头巾，他们封杀了摇滚和朋克，他们绞死共产主义者。内贾德在哥伦比亚大学演讲时说伊朗不像美国，没有同性恋者，也许，我真的是不理解这个国家总统的黑色幽默。 <br />
　　民主自由的国家都是相似的，不民主的国家却各有各的不同。把托尔斯泰翻来倒去，倒是挺能反映现实。 <br />
　　 <br />
　　自由，那总是让人感到血脉喷张。不管是关于爱情还是婚姻，理想还是信仰。 <br />
　　年轻时的玛赞曾在信仰里犹豫徘徊，也曾被爱情刺伤。夜色冰凉的维也纳，她像一只被抛弃的小猫一样落魄流浪。后来在德黑兰，她更是在那段让爱情干涸的婚姻里近乎绝望。可是她伟大的家庭却一次次的告诉她，孩子，没有什么可以束缚你，你梦寐以求的，是真爱与自由――就像郑钧在那首叫《私奔》的小曲儿里狂野的歌词。 <br />
　　 <br />
　　“孩子，我要你自由，去找你自己的生活，再不要回家。” <br />
　　玛赞妈妈在机场的话差点让我泪下。那一刻的画面，就像老鹰把雏鹰扔下悬崖，所有的这些，只是出于一种希望，希望她能够独立而自由的去飞翔。奶奶也曾对怀抱里的玛赞说，孩子，不论生活多么艰难，我们都要活出自己像茉莉一般的芬芳。 <br />
　　 <br />
　　恩格斯说，哪里有黑暗，哪里就会有太阳。哪里有压迫，哪里就有反抗。Punk is not dead，在德黑兰的街头，在课堂，在委员会的例会上，为了自由，那是玛赞最初的反抗。 <br />
　　 <br />
　　自由，对于我们来说，从来都不是存在主义里高深的哲学，也不是娑婆世界里难以体察出的禅意，更不是跃出麦田守望者的悬崖。其实和玛赞的伊朗一样，我们至今仍生活在某种意识形态的空想里，我们甚至无法自由的表达自己。安定让我们或许麻木，但我们没有忘记，我们的反抗，曾经那样激烈的发出过光芒，在一九八九，在那个庄严的广场。 <br />
　　 <br />
　　布拉格之春后，捷克人说生活在别处。是的，生活，其实在理想栖息的地方。 东边有个中国，西边有个伊朗。就像玛赞奶奶在海边说的一样，对我们而言，自由和生活仍在别的地方。 <br />
