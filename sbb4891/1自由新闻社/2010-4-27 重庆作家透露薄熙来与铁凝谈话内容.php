<br />
这位作家称，铁凝当上部级作协主席后，尝到了权力的滋味，很想更上一层楼，从而本性大变。而在和薄熙来的对话中，这位以前挺淳朴的女作家看到了希望。据说，薄熙来在谈话中表示很惊讶铁凝如此年轻，他鼓励铁凝要把作协领导好，说她“大有希望”。<br />
<br />
