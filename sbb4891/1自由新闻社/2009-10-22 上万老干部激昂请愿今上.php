																											<div id="postmessage_294841" class="t_msgfont">共军军、师职老干部第二次万人请愿书<br />
 <br />
　 尊敬的胡锦涛总书记和中央政治局常委会并源潮部长： <br />
<br />
<br />
&nbsp; &nbsp; 　　军队离退休老干部和遗属，年事高危，是一个正在被遗忘走向衰亡的弱势群体，他们曾经是血战沙场创建新中国抗美援朝战争的一代勋臣，但是，多年来他们的身心和晚年生活并不愉快，不是缺钱花，也不是缺饭吃，而是遭遇不公平不正义的对待，人格尊严受辱，饱尝住房困难、医疗困难两大生存危机困扰之苦，当此新中国六十周年华诞之际，向总书记，向党中央，上书请愿。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　我们的诉求： <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　一、反对“住房货币补差”，呼唤“军队老干部住房保障”。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;　　何以反对“货币补差”?因为侵权非法、不公平不正义、违背党中央“生活待遇从优”的决定、违背国家“离休干部住房标准与在职干部相同”的政策、违背胡主席 “搞好军队老干部住房保障”的指示、与改善老干部恶劣住房条件背道而驰、强迫老干部接受不民主、歧视坑骗老干部吃大亏、侵犯老干部的权利和尊严。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　(1)“货币补差”，十年前由军队大贪腐败类时任总后营房部长王守业一手炮制，为压迫老干部而出笼，是歧视老干部的“三不政策”――“不许退房、不补面积、不享受经济适用房补贴、所差面积货币补差”的核心政策，把老干部依法理应享受的住房面积、住房待遇和住房权利，用命令手段，低廉的价钱，强制买断，非法剥夺老干部受到法律保护的住房面积和住房待遇，侵犯了老干部的住房权利和人格尊严，把老干部打入“房改”冷宫，并以军委前领导人签批的军委十九号文件的名义，压制老干部永世不得翻身。这就是所谓“货币补差”不光明的来历，也是广大老干部强烈反对坚决抵制的根本所在。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　动用军委名义改变党中央决定压迫老干部，以军压党，违背全党服从中央的根本原则。军委无权改变党中央的决定，更不允许取代党中央、站在党中央之上发号施令贩卖私货，从而公然触犯、践踏了党中央的决定、人大常委会的立法、国务院及老一辈军委的政策，因此，广大老干部有理由有权利不接受强加在头上的虐待和欺侮。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　(2)“三不政策”、“货币补差”，压迫老干部十年苦不堪言，师职离休老干部绝大多数被压在7 0平方米(套内面积)、设施简陋、质量很差、无电梯危房艰难度日，特别是抗震性能极差，砖结构，豆腐渣工程，一旦发生四级以上地震，数万老干部住房就将成为埋葬他们全家老小的活棺材，四川汶川大地震的惨痛教训更给困居危房的广大老干部的生命安危敲响丧钟! <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;　　十年国家改革发展的巨大成果，可喜可贺，但是，就军队住房状况而言，实为在职当权干部所独享，军委四总部带头，有权有钱，大规模建造师职160― 200平方米、军职220―300平方米、抗八级裂度“精品楼”、豪华楼，造成新、老干部住房面积和住房待遇的巨大差别。更有甚者，就在汶川八级特大地震巨大国难的同时，在北京最美的地域――钓鱼台西、玉渊潭公园北，建造军委领导人别墅群，其豪华当数世界殊，其坚固天上可防原子弹，地下可防八级地震，两层楼加电梯，为扩大后花园，竟然侵占海军总医院太平间、遗体告别室，有人说，这是与死人争地盘，搞豪华保命安乐窝。据有关人士披露，这座别墅群，耗资慷慨，侵占巨额国防费，与民争利，与死人争地。现军委委员以上领导人，早已在500―700平方米豪宅安居，为什么还建军委领导人别墅群？把军委领导人的身家性命放在党中央、国家和全国人民的命运之上符合党的建军原则吗?<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　这样搞法，实际上是推行一军两制，打天下的老革命，拥挤在三世、四世同堂的“贫民窟”，坐天下年轻的当权派享用大户型精品豪宅，这种新老同级干部住房待遇的“天差”，在建国60周年之日时，怎能不让老革命们倍感尊严受辱？更有多少开国老功臣在困境期盼中抱憾离开人世!<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　(3)十午后的今天，又以“关怀”为名强迫推行的“住房货币补差”，是真关怀吗?是真补差吗?铁的事实证明，“聪明’’、“机智’’的总后施政者，把老干部当傻瓜，费尽心机，在“关怀”背后埋伏陷阱，暗藏杀机，推销愚人、骗人、欺人、坑人政策，干违法、违纪、霸产、坑害、掠夺勾当而大显身手，让相当多的老干部在混沌中被迫上了贼船，上当受骗吃大亏。因此，我们理直气壮地说：总后强迫推行的“住房货币补差”，连半点“关怀”也没有，以“补差”为幌子，行克扣搜刮之实，以剥削者压迫者的面孔“吞食” 老干部，背叛党和国家关怀老干部的优待政策。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　2009年2月18日，是一个值得彪炳军史的日子，这一天全军万名老干部和遗属，向总后领导人，向军委，向胡主席，上书请愿，表明了反对“货币补差”的严正立场。3月下旬、4月上旬，再致廖锡龙部长公开信和致胡主席的信，明确指出“货币补差”包藏十大坑人陷阱，是一个掩盖陷阱坑害老干部的骗局，即便对簿最高法院公堂，坚信真理和正义在老干部一边。但是，万人请愿，老干部的正义呼声，招来压制和冷眼相待。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　(4)“货币补差”是为老干部“提供良好服务保障”吗?<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　以北极寺几位老干部为例，请看结局：<br />
&nbsp; &nbsp; <br />
