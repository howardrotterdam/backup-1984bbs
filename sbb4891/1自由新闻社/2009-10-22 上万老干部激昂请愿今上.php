&nbsp; &nbsp; 　　(1)为什么“补差”?补差目的是什么?”“补差”是什么性质行为?<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;　　首先因为总后依法拖欠老干部理应享受的住房面积，即住房待遇和住房权利，欠债要还，差额要补，欠什么还什么，差什么补什么，天经地义，即如此，总后依法拖欠老干部的住房面积，就应当依法补够所欠住房面积，只有欠什么补什么，欠多少补多少，才能保障老干部的住房待遇和住房权利百分之百享受，因此，面积补差是 ―个政策行为，是老干部待遇和权利的实现。“吞食”、“宰割”老干部的住房面积，不管用什么手段，都是对老干部待遇和权利的侵犯，是违法行为。我们要质问：总后为什么不依法给老干部面积补差？<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　(2)“住房面积补差”的本质是什么?为什么强迫推行?<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;　　从表面看，总后出几个小钱，把依法所欠老干部的住房面积买回去，堵上这笔欠账。其实，这是一个掩盖强权、廉价、垄断的不公平交易。即是交易，最起码的原则要两厢情愿，请问总后老爷们，你们同老干部一方商量过吗?老干部同意吗?在你们眼里，这一切都没有必要，因为你们手里握着强权，强权就可改变一切解决 ―切，所以你们霸道单方强迫推行，欺行霸市，横征暴敛。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　必须严正指出：住房面积标准的本质，是老干部待遇和权利的象征，是党的政策的体现，请问待遇、权利、政策能够交易吗?你们以为钱就可以把老干部的待遇、权利买到手?不！你们错了，哪个老干部情愿出卖依法享有的待遇和权利?你们就是给金山银山老千部也决不会以待遇、权利和党的政策去作这笔交易。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　总后勤部官老爷们不顾老干部强烈反对，抗拒党和国家老干部政策，一意孤行强迫推行住房货币补差，如此恶意坑骗老干部，势必把老干部“逼上梁山”。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　(3)以什么标准补差?<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　补差，差多少?补多少?应有合理标准，就我军干部队伍住房历史和现状看，至少有四个标准：<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;　　第一个标准出于上世纪七、八十年代，为安置离休干部设立，至今依然只有离休干部受管束。第二个标准，1999年9月出台，师职105―120平方米，实际上是―个空壳，在职师职干部普建160-200平方米经济适用房，根本没有执行105―120标准，离休干部所差面积也没有补差。第三个标准，总后营房部于2002年发布“套内面积标准”，即除阳台、公摊走廊面积外的使用面积，正师120副师105平方米，实际上为在职干部专享，离休干部被排斥在“套内面积标准”权利之外。第四个标准，即在职干部多年实际居住的面积标准，师职160-200平方米，同样为在职干部所独享。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;　　这是人为推行一军两制不公平的政策造成的巨大差别，破坏了军队的团结统一，我们强烈要求：第一，总后应当立即承认离休干部与在职干部一样同享“套内面积标准”的权和]，按“套内面积标准”给离休干部补够所差面积。第二，消灭十年来造成的离休干部与在职干部住房待遇事实上巨大不平等。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　以总政为例，请看总政直工部建房公告：由军委徐才厚副主席李继耐主任亲自挂帅、督战，在北京西直门外交大20号为总政机关建造四百套师职“精品楼”，最大户型使用面积178平方米。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　以总参为例：在官园建造175平方米师职豪华楼，又在小西天管保部院内建造189平方米师职京城顶尖豪华楼。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　离休师职干部现住房，在面积上质量上无法与同级在职干部相比，面积差成倍，抗震裂度差成倍，在职干部住房可抗八级裂度，离休干部住房，大多数是破旧危房，四级裂度就将楼毁人亡。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　消灭新、老干部住房巨大差别刻不容缓，最现实的办法，就是改变老干部现住房的低标准，提升到150-160平方米，与同级在职干部现住房面积标准基本持平，国家具备财力条件，新老干部都能比较满意接受。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　使我们无法理解的是，总后为什么总在压制老干部住房面积?面对总后高价强卖，为什么对在职干部兴建大户型精品豪宅大开绿灯?为什么继续保护、扩大差别?<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　(4)在职干部完美享用大户型“精品楼”，并且还享受高额住房补贴，为什么困居破旧危房里的老干部却不享受住房补贴?为什么要推行富上加富、穷上加穷的政策?为什么离休干部不能与在职干部同享住房补贴? <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;　　(5)1997年、1999年12月，军队老干部两批按成本价购房，得到百分之百产权受到法律保护，10-12年后，不知总后依照什么特权强制收回老干部的产权霸占老干部房产?总后又是根据什么特权擅自把老干部的房产数十倍提价再强卖给老干部?难道老干部只有逆来顺受就没有拒绝购买的权利吗?<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;　　(6)这次住房货币补差，总后玩的另一花招，就是以旧充新、以危房冒充经济适用房，以当今经济适用房最高价3960元／平方米强卖给老干部，老干部支付 40万元得到的是自己居住17年有22年房龄无电梯危房，请问，总后为什么以旧房充新房?为什么以危房冒充经济适用房?<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　再问，老干部拿的是经济适用房新房高房价，为什么不让老干部享受经济适用房“套内面积标准”?为什么不兑现经济适用房抗八级裂度的质量保障?<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　再问，老干部出资40万元，完全可以买回经济适用房新房，即便新建160平方米经济适用房，按2000元／平方米造价，仍绰绰有余，总后的同志们为什么不给老干部建经济适用房? <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;　　再问，总后以旧房高价3960元／平方米强卖给老干部的同时，又以12年前1150元／平方米低价从老干部手中强制买回依法属于老干部的补差面积，每平方米差价2810元，公平吗?合理吗?据市场调查证明，十多年来北京房价上涨10倍以上，1997年1150元／平方米。如今已涨价 15000-23000 元／平方米，总后只花1150元就强制买走老干部一平方米补差面积，不及市场价二十分之―。另据近日北京市出台房屋拆迁政策，按市场房价给拆迁户补偿，而总后为什么这么凶狠压低老干部的房价?请问，面对总后低价强买老干部的补差面积，老干部有没有不出卖的权利?<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;　　(7)上世纪九十年代军队老干部购房，按国家规定，在地方工作的家属纳入军购，与丈夫共享产权，十年后的今天，总后突然变脸，撕毁夫妻双方一处购房共享产权的国家规定，把享有百分之五十产权的老干部家属从“住房货币补差”―脚踢开，剥夺了参加军购家属的住房待遇和权利，弄得她们军、地两空。北极寺有― 位老干部在地方工作的家属刘泽英，当年她未参加军购，原单位给她补发20多万住房款。请问，总后凭什么剥夺纳入军购与老伴共享产权的家属的住房待遇和权利?让她们去找谁?她们的重大经济损失由谁承担?<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　(8)总后根据什么原则规定1999年前去世老干部的遗属无权参加“住房货币补差”?这个规定毫无道理，不只是对遗属的歧视，更是对早逝老干部的鞭尸行为，让我们仍活在世上的老干部感到心痛，也为早逝的老战友感到悲伤，这样对待早逝老干部是极大不公，是完全丧失人性的恶作剧。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　(9)总后从老干部手中夺走产权，又以 3960元／平方米高价第二次强卖给老干部，自每位老干部身上搜刮37万-40万元，全军师职离休干部四万计，仅此 ―项，总后就从老干部身上敛财一百多个亿，过去的历史大资本家发战争财，而今总后勤部千方百计发老革命的财，你们的良心何在?你们还是共产党的总后勤部吗?<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　(10)据有关人士估计，离休老干部尚有―笔相当数额的住房公积金不知去向，不知何时归还老干部?也不知是否又被暗中没收?<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;　　(11)解放时期老干部的13个月工资问题，不能再拖了，老红军、老八路多年来都享受着13个月以上的工资待遇，抗美援朝老干部享有工资补贴，就连年轻在职干部也享有12个月以外―个月“奖励工资”，为什么惟独把参加解放战争打天下的老干部排除在外?打解放战争有罪吗?论对人民对国家的贡献，他们不如坐天下年青的在职干部吗？有人借口解放战争时期老干部人数太多，国家没钱。讲这种话没有政策观念。中国共产党从来没说打解放战争人太多了。解放战争是中国两种命运大决战，没有解放战争的胜利，就没有新中国，新中国是枪杆子打出来的，不是从天上掉下来的，论功行赏也应有他们―份重奖。是国家没有钱吗?2万亿美元储备，一年9000亿“三公”消费，为什么对待打天下的开国功臣这么抠门吝啬?<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　四、要求兑现人大常委会立法 恢复1953年前入伍师职干部离职休养待遇。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;　　建国初期入伍的师职干部，经历过严酷的抗美援朝战争，1978年经全国人大常委会批准退役后可以“离职休养”。1982年发生变故，中央几位领导人担心转业干部“攀比”不顾法律程序，不经人大审议，改作退休处理。我们认为，这样作法不妥。宪法明确规定：“任何组织和个人都不得有超越宪法和法律的特权 ”。“ 党章”明文规定：“党必须在宪法法律范围内活动。”因此，中央几位领导人的意见不能改变或取代人大常委会的立法决议。领导人站在法律之上干预司法，是对人大常委会立法权威的侵犯，是违法行为。我们要求中央领导机关和领导人以法行政，以法兑现1953年底前入伍师职干部“离职休养”待遇。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　坚持我们的行为是正义的，是为维护党和国家老干部政策的尊严，是为维护党和国家赋予老干部的合法权益，是为反对歧视老干部呼唤公正义和谐而抗争。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;　　打天下创建新中国的老一辈幸存者，再也不愿忍受“光环”下的屈辱，呼吁党中央过问，期待党中央关怀。总后勤部不改变压老、坑老、拖老办法，继续抗拒中央的老干部政策，老干部别无选择，舍得一身，誓将在新中国六十周年近期，以适当方式、适当地点表达请愿诉求，呼唤伟大、光荣、正确的中国共产党！呼唤党和国家老干部政策的尊严！呼唤公平正义的人权保障！<br />
