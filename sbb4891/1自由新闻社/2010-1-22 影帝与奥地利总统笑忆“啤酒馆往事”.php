																											<div id="postmessage_446179" class="t_msgfont">温家宝与奥地利总统笑忆“啤酒馆往事”（图）<br />
<a href="http://www.china.com.cn/international/txt/2010-01/22/content_19287366.htm" target="_blank">http://www.china.com.cn/internat ... ontent_19287366.htm</a><br />
<img src="http://image1.chinanews.com.cn/10p/PhotoN/100121/100121204ta_3.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
1月21日，中国国务院总理温家宝在北京钓鱼台国宾馆会见奥地利总统菲舍尔，二人手持1988年时任中共中央书记处候补书记、办公厅主任温家宝同时任奥地利国民议会社会党议会党团主席菲舍尔等在维也纳的合影照片不亦乐乎。中新社发 廖攀 摄<br />
<br />
中新网北京一月二十一日电题：温家宝与奥地利总统笑忆“啤酒馆往事”<br />
<br />
记者 张朔<br />
<br />
“我们晚上十点以后干了什么事，我们谁都不告诉！”<br />
<br />
今晚，北京的冬日寒风也挡不住两位老朋友重逢的热情――中国总理温家宝与奥地利总统菲舍尔在钓鱼台养源斋的会面，一开场就笑声不断。<br />
<br />
傍晚六时许，菲舍尔走进这座逾百年历史的庭院。在此等候的温家宝笑着伸出手：“老朋友你好！”菲舍尔则给中国朋友一个热情拥抱，并赞叹道：“这里真漂亮！”<br />
<br />
宾主落座后，温家宝说：“你是我的老朋友了，我有件礼物可以证实。”<br />
<br />
温家宝的礼物，是一幅二十二年前访奥时与菲舍尔在维也纳一家啤酒馆里的合影。<br />
<br />
菲舍尔惊喜地站起身来，与温家宝一起端详着这张老照片。<br />
<br />
“您从哪儿找到的这张照片……这是彼得・席德尔先生，他的儿子现在是国务秘书，也在我的代表团里……当时是十二月吧？”<br />
<br />
说到这里，菲舍尔忽然“得意”地表示：“总理先生，我也有一件信物。”<br />
<br />
菲舍尔的“信物”，是他一九八八年的年历。<br />
<br />
他翻开当年十二月五日那一天，念道：上午十点钟接待中国代表团，十二点三十分和中国代表团共进午餐，晚上七点和代表团共进欢迎晚宴。<br />
<br />
听到这里，温家宝笑着插话：“但您忘记了一点，那是晚上十点钟喝的啤酒。”<br />
<br />
一片笑声中，年过七旬的菲舍尔风趣地说：“<strong>我们晚上十点以后干了什么事，我们谁都不告诉！</strong>”<br />
<br />
这是菲舍尔就任总统后首次访华，带来了包括数十名官员、一百二十余名企业家的庞大代表团。不过，早在一九七七年，他就曾来到中国。今天，他还把那次访华时的照片作为礼物送给温家宝。<br />
<br />
“总统先生是中国人民的老朋友。”温家宝说，“我们从二十二年前就能够坦诚地交换意见。我希望，我们现在一如既往，能够为中奥关系的未来、为中欧关系的未来进一步贡献力量。”<br />
<br />
温家宝说，奥地利是欧盟重要国家，也是中国的友好合作伙伴。两国和两国人民都崇尚和平，尊重彼此选择的发展道路，致力于加强不同文明之间的对话。这对增进中欧之间的相互理解和信任，推动国际社会合作应对各种挑战具有重要意义，也使中奥关系超越双边范畴，具有更广阔的发展前景。<br />
<br />
他并指出，中方愿同奥方加强在各领域的平等对话与互利合作，推动两国关系和中欧关系健康稳定向前发展，为促进世界的和谐和可持续发展作出新的贡献。<br />
<br />
菲舍尔则表示，奥中两国在许多问题上拥有广泛利益和相同看法。进一步发展中奥关系符合两国的根本利益。奥方愿与中方共同努力，推进奥中关系不断迈上新台阶。<br />
<br />
会见后，温家宝还为菲舍尔一行准备了欢迎晚宴。从二十二年前的维也纳啤酒馆，到今晚的养源斋，两位老朋友再次为双方友谊写下难忘的一笔。<br />
文章来源: 中国新闻网</div>

							
							
							
							
