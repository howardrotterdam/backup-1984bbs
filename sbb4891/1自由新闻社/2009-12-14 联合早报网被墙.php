																											<div id="postmessage_372851" class="t_msgfont">中国法律沦为权力工具？来源：《联合早报》　作者：韩咏红　时间：2009-12-14<br />
<br />
成都金牛区女企业家唐福珍自焚事件，已过去数个星期。对于事件本身，中国主流媒体从上周以后就进入噤声状态，相关激烈讨论于是迅速转移到网络上。<br />
<br />
讨论不止，因为事情还没有结束，唐福珍在拆除大队向自己的房子步步进逼的时刻，以自焚的惨烈方式祭出了生命。事情闹大了，在舆论激烈抨击声中金牛区城管局长被撤职，可是这个显然意在平息民怨的动作，并没有改变金牛区政府对唐福珍“暴力抗法”的定性。而今，唐家数名亲属在痛失亲人之余，还因涉嫌妨害公务被刑事拘留。<br />
<br />
学术界知识分子拒绝坐视，除了有学者提笔痛批当地政府无耻，北大五名著名法学家上周向全国人大常委会递交了建议书，要求人大修改《城市房屋拆迁管理条例》（简称《拆迁条例》）。据报道，有关修改《拆迁条例》的建议，早在一年前就被提出，而且还提过2008年完成修改的时间表，但至今音讯杳然，原因是受到地方政府的阻力。<br />
<br />
就唐福珍一案而言，她一家人是否存在“抗法”的行为，以及与《拆迁条例》是否相关，这是当地政府与舆论争辩的要点。按金牛区城管执法局的说法，她们家的三层楼房本来就是证件不齐，属于违法建筑，将之除之不是叫“拆迁”，是“拆违”，政府行为“主体合法，程序合法”。<br />
<br />
不过，今天的这些争辩却没有办法改变另一个事实背景：即有关楼房从1996年以后已经合理地存在了11年，而且还是地方政府当年招商引资时，将唐福珍与夫婿说服回当地开业所设的厂。直到2007年出现新市政建设工程规划，需要利用有关土地时，“违法”的属性才被提出来，充作政府将之拆除的“合法”理由，最后再进一步变成指控唐福珍一家“暴力抗法”的依据。<br />
<br />
从某个角度来说，唐福珍悲剧的根源确实与《拆迁条例》没有直接的关系，其根源是条例能够任由地方管理部门“呼之则来，挥之即去”，可用可不用，管理部门有需要时就亮出来执行。而从另外一个角度说，唐福珍的自焚与其他拆迁悲剧都有共通之处，他们都透露出在中国城市大发展、大规划的过程中，现行制度、条例对民权的保护严重不足。权力高度不平衡，在强大的官权面前，民权薄弱无力。<br />
<br />
而让人警惕的是，中国在建设现代国家、强调“依法治国”的时候，依管理部门意志使用的“法”，结果是加剧了民权与官权天平的倾斜，在唐福珍的案例中，“法”是一种单向的，以上对下，官方管理民众的工具。<br />
<br />
中国在过去十几年里，形成了以投资、出口驱动的经济发展模式。这个模式日益牢固，如今房地产投资占了国内固定资产投资20%，国民生产投资的10%。而在另一方面，在中央与地方分税制改革以后，卖地成为地方政府主要的收入来源，地方政府有了卖地趋利的动机，又握有不平衡的征地权力，这就造成全国各地拆迁冲突总是此起彼伏，连续不断。<br />
<br />
关于改革、关于限制官权的迫切性，这个论点中国国内学者早已反复论述过多次。本届中国政府大力提倡“依法治国”，本意也在于达成对权力进行约束的目的。至少，“依法治国”要求权力必须在法的框架下运行，而且它给了社会一种期望：官、民都面对“法”的平等约束。<br />
<br />
因此，对失去生命的弱者施以“暴力抗法”指控，给社会的刺激尤为强烈。法律成了权力的直接工具，“暴力抗法”说得越振振有词，越是打击已然脆弱的社会信任。至今，“自焚”事件看似已不了了之，没有更高级的官员为此负责，但是民众的不信任感不会不了了之，只会继续加剧。所谓“依法治国”、“以人为本”的诚意，正在面临考验。</div>

							
							
							
							
