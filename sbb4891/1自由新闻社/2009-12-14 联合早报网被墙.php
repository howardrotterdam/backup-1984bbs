　　拥有个人网站，并且活跃于互联网的北京理工大学经济学教授胡星斗受访时指出，政府对于打击网络低俗从来是重拳出击，当局近期对信息的管制更严，其实包含了更深的政治背景。他说：“这是一个非常复杂的问题。为什么最近对互联网信息审查更严，这与中国政府在政治上越来越走向保守，在经济上越来越走向国有垄断；在政治上去法治化，在经济上去市场化有关。”<br />
<br />
　　他认为，改革开放30年来，中国社会积累了许多深层矛盾，必须通过政治体制改革才能加以解决。但高层又缺乏改革的决心，因此它最终采取对经济资源、言论空间加紧控制的措施来保持稳定，是一种必然的结果。<br />
<br />
