 <br />
中国整治互联网引发舆论哗然 <br />
中国加紧对网上活动的“严打”，尤其是关闭一系列BT网站的行动，在互联网群体间引起哗然。 <br />
<br />
● 韩咏红<br />
北京特派员<br />
<br />
　　中国政府与互联网使用者的拉锯战进一步拉高。在今年以来连番的打击低俗行动下，公安部门宣称一共关闭了6972个淫秽色情网站与栏目，删除信息125万余条，抓获嫌犯3470余人。但是民间活动人士认为，加紧“扫黄”之余，政府也在通过一系列的行动加强互联网监控，压缩言论自由空间。<br />
<br />
　　11月下旬以来，中国公安部在官方媒体大张旗鼓的宣传下，开始新一轮针对不良网站的整治专项行动。据报道，这项行动剑指淫秽手机色情网站，因为手机色情网站已经成为对青年人思想健康的很大威胁。<br />
<br />
　　与此同时，广电总局着手整顿不具备《信息网络传播视听节目许可证》的视听节目服务网站。影响之下，中国著名的影视片下载服务网站“BT中国联盟”被关停，经常被网民用以下载美国电视剧与影片的VeryCD也神秘“休克”了一天。<br />
<br />
　　据有关部门的介绍，“整治互联网视听服务是一项长期工作”，目标是打击国内的色情节目下载与侵权盗版货，“只要被关停网站不解决侵权盗版、传播低俗色情节目等问题，就不能重新开放。据介绍，过去两年来，广电总局共关停530多家视听节目服务网站。<br />
<br />
　　刚过去的周末，中国网民发现他们又面对一项新的限制：个人用户不准进行域名注册。<br />
<br />
　　这项条例周六由中国互联网络信息中心（CNNIC）发出公告宣布，今日（14日）开始生效。对于改变的原因，互联网络信息中心并没有解释。<br />
<br />
　　政府加紧对网上活动的“严打”，尤其是关闭一系列BT网站的行动，在互联网群体间引起哗然。像“BT中国联盟”这样大型BT网站，其每天的IP访问量，据统计网站点击率网站alexa.cn的统计是超过25万个，浏览量超过80万次，这些网站的关闭意味着中国网民享受免费下载影视剧的时代的终结。<br />
<br />
