													<h2>联合早报被墙文章</h2>
						
						
																											<div id="postmessage_374336" class="t_msgfont"><a href="http://www.zaobao.com/yl/yl091209_002.shtml" target="_blank">http://www.zaobao.com/yl/yl091209_002.shtml</a><br />
<br />
首页 &gt;&gt; 观点 &gt;&gt; 今日观点<br />
<br />
中国民主化是两岸和平统一的最佳途径<br />
<br />
● 薄智跃<br />
<br />
中国聚焦<br />
<br />
　　在两岸关系诸多选项中，台独是没有出路的；维持现状是不可能的；大陆的民主化会增加武力统一的变数；而只有实行包括两岸四地的中国的民主化才有可能实现两岸的真正和平统一。<br />
<br />
台独的出路<br />
<br />
　　目前，台独的力量仍然存在。民进党虽然在2008年大选中失败，但在上星期六的县市长选举表现不俗。不仅守住了三个县(云林县，嘉义县，屏东县)，而且还夺回了“民主圣地”宜兰县。另外，和2005年县市长选举相比，民进党的得票率增加三个百分点，从42%提升到45%。民进党作为台湾第二大党，仍然有卷土重来的可能。民进党最近虽然在对大陆政策上略有调整，可是它以建立一个“台湾共和国”为最终目标的基本主张并没有改变。同时，民进党在民间仍然有很大的影响。在2008年大选中，民进党候选人谢长廷获得544.5万张选票，占总有效票数的41.6%。而支持以“台湾”的名称加入联合国的选民则达552.9万。换句话说，台独的力量甚至大于民进党的力量，因为支持以“台湾”的名称加入联合国的选民比支持民进党候选人的选民还多83991人。<br />
<br />
　　但是，台独是没有出路的。首先，台湾的大多数老百姓不同意。民进党一手操纵的“入联公投”2008年遭到台湾大多数选民的否决。今后再提出这样议题的概率大大减少。其次，大陆的老百姓坚决不答应。绝大多数中国人认为台湾是中国的一部分，两岸统一是迟早的事。第三，没有一个中国领导人敢在这个问题上三心二意，因放纵台独势力而背千古罪人之名。最重要的是，台独在国际社会上没有市场。“中华人民共和国是中国的唯一合法政府”是1971年以来国际社会的共识。今后改变这一共识的可能性越来越小。所以说，台独是没有出路的。<br />
<br />
维持现状<br />
<br />
　　在台湾，有相当一部分人认为保持现状是最佳选择。台湾曾经是世界上最耀眼的经济明星之一，有着骄人的经济发展业绩，为亚洲四小龙之首。在过去的十几年中，台湾又经过了民主化的洗礼，成为亚洲的一盏“民主明灯”。<br />
<br />
　　但是，应当看到台湾所面临的问题十分严重，维持现状是不可能的。概而言之，台湾的问题可归纳为国际上的边缘化，经济上的空心化，社会上的撕裂化。在国际上，台湾的国际空间随着时间的推移只能是越来越窄。在国与国的关系上，“台湾”没有一个邦交国。“中华民国”目前有23个邦交国。这个数目增加的可能性极小，减少的可能性极大。在加入国际组织方面，“台湾”和“中华民国”都没有份儿。目前，台湾参与国际组织活动的英文名称是“China Taipei”，而中文的翻译为“中国台北”或“中华台北”。在与大陆不统一的情况下，台湾在这方面的发展余地也不会很大。尤其是大陆领导人会担心，这方面的过多让步会在国际社会上造成一种不可逆转的“一中一台”或“两个中国”的事实。<br />
<br />
　　台湾的经济在过去几十年逐步空心化。许多重要企业都已经到大陆发展，本地产业急剧萎缩。2008年从美国开始的全球性金融和经济危机，使马英九先生的“633”政见承诺彻底泡汤。在全球经济一体化迅猛发展的今日，与世界经济发展脱轨会使台湾经济大幅度下滑。<br />
<br />
　　在陈水扁主政期间，台湾社会之间的分歧被人为的夸大，造成族群分裂。马英九上台以来，一直在想方设法弥合各个族群、各个党派、各个团体之间的分歧，虽略有成效，但没有根本改变蓝绿对立、南北分野的格局。<br />
<br />
　　单从经济发展的角度看，台湾也不可能维持现状。2010年1月1日，中国-东盟（亚细安）自由贸易区正式启动。一个没有完全融入“中国”的台湾自然是没有份儿的。由于缺乏与各国进行经济、贸易交往的有效机制，台湾已从亚洲四小龙之首降为四小龙之尾，并将继续拉大与其它国家和地区的发展差异。<br />
<br />
大陆的民主化<br />
<br />
　　台湾一些学者认为，两岸最终统一的症结在于双方政治制度的差异，两岸统一的前提是中国大陆民主化。他们的想法有一定的学术背景。国际学术界的确有一种观点，认为民主国家之间不会发生战争。但是他们忽略了民主的一个重要属性，那就是，民主的民族主义基础。<br />
<br />
　　在当今的大陆，民族主义倾向十分明显。随着中国的发展，说“不”的和“不高兴”的“愤青”越来越多。在这样的环境中，经过民主选举产生的领导人更有可能代表极端的民族主义倾向。为了争取这些选民的支持，一些政治家完全有可能以“两岸统一”为承诺。而一旦选上，“两岸统一”就会成为选民对他们的授权(mandate)。如果“统一”是一个有时间限定的目标，武力解决就成为最为有效的方式。<br />
<br />
　　邓小平1980年1月就把“台湾归回祖国，实现祖国统一”作为八十年代的三大任务之一。再过几天就整整三十年了，但这一目标仍然没有实现。其原因就是因为中国领导人一直在努力实现“和平统一”。但一个有选民授权的大陆领导人绝不会再等三十年，而“武力统一”的变数则会大大增加。<br />
<br />
中国的民主化<br />
<br />
　　两岸要想真正实现和平统一，实行中国的民主化才是最佳途径。我这里所说的“中国”，不是狭义上的“中国”(大陆)，而是广义上的“中国”(23个省，4个直辖市。5个民族自治区，2个特别行政区) (笔者曾于2004年7月13日在“联合早报”发表“全新的‘一个中国概念’”一文对此有所阐述)。中国的民主化自然是在包括两岸四地的全中国范围内进行的。<br />
<br />
　　在这一过程中，大陆、台湾、香港、澳门的政党都可以参加全国性的政治角逐，有可能成为中国的执政党。应当说，中国共产党将会是最有竞争力的政党。中国共产党有将近7600万党员，具有在大陆执政60年的丰富经验，尤其是在过去三十年取得了举世瞩目的经济发展奇迹。中国国民党也有很强的实力。中国国民党有近百年的历史，有党内民主和竞选民主的经验。民进党如果放弃台独主张，改名为“中国民进党”，将来也会有前途，因为它在基层民主选举方面的经验也十分丰富。<br />
<br />
　　如果各个党派不是在通过战争来角逐，而是通过选票来竞争的话，那么，中国的统一自然是和平的。如果中国共产党通过选举成为中国的执政党，台湾人民肯定会心悦诚服。如果中国国民党能够通过竞选成为世界上最大国家的执政党，大陆老百姓也会期盼出现一个廉洁、高效的政府。<br />
<br />
　　这样，一个团结、统一、和谐、民主的中国将屹立于世界的东方，成为世界各国人民尊敬和向往的地方。<br />
<br />
作者是新加坡国立大学东亚研究所资深研究员，文章仅代表个人观点。</div>

							
							
							
							
