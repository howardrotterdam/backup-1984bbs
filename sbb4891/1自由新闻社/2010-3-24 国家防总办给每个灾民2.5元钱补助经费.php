																											<div id="postmessage_529992" class="t_msgfont">国家防总办:中央已下拨1.55亿抗旱补助经费<br />
2010-03-23 17:39:24　来源: 新华网　<br />
核心提示：23日，国家防汛抗旱总指挥部办公室副主任邱瑞田称，财政部和有关部门,中央下达了1.55亿抗旱补助经费,主要是应对这些省的人畜饮水问题。<br />
<br />
新华网3月24日报道 3月23日,国家防汛抗旱总指挥部办公室副主任邱瑞田就当前我国西南等地旱情和防汛抗旱情况与网友进行在线交流。<br />
<br />
[主持人]我们知道国家防总一直是防汛抗旱的专门职能部门,这次旱情发生以后,从咱们国家防总层面来说都做了哪些工作?<br />
<br />
[邱瑞田]面对当前的旱情,党中央、国务院领导是相当关注,党中央领导、国务院领导多次做过批示,温家宝总理、回良玉副总理也亲赴旱区视察旱情、体察民情。 <br />
<br />
国家防总作为防汛抗旱的一个职能部门,也始终在关注着当前的旱情。我们从今年年初,确切地讲从去年年底就开始,对西南地区诸省的旱情就给予高度关注。 <br />
<br />
首先,加强了对降雨和水文的观测和分析。就是说对当前和过去一段时间的来水量、蓄水量进行一些监测,同时也非常关注着我们的气象预报。 <br />
<br />
<br />
其次,当旱情发生以后,国家防总、水利部给予了高度关注,我们国家防总副总指挥陈雷同志多次召开会商会,研究怎么应对当前的旱情。同时,我们大约派出了18个工作组分赴这几个省,帮助地区分析这些灾害发生的原因,找出应对的措施。 <br />
<br />
再次,财政部和有关部门,中央下达了1.55亿抗旱补助经费,主要是应对这些省的人畜饮水问题。 <br />
<br />
第四,我们也正在商有关的部门,比如发改委、财政部,比如对今年的人畜饮水困难,还有灌区改造,一些涉及水利方面的问题,能不能早做计划,早下拨资金,把应对今年的干旱和正常的水利基本建设衔接起来。通过采取一系列的措施,我想会有一个很好的起色,会有很大的变化。 <br />
<br />
