																											<div id="postmessage_574656" class="t_msgfont">中新网4月21日报道 89岁的前国际奥委会主席萨马兰奇20日中午因心脏病在西班牙巴塞罗那市住院。医院发言人表示，萨马兰奇的病情非常严重。<br />
<br />
院长说，萨马兰奇是因为急性冠状动脉供血不足，情况很危急。<br />
<br />
<br />
近年来，萨马兰奇健康一直出问题，去年十月，他受邀出席国际体育电视节时心脏病发作，被送入摩洛哥一家医院，接受治疗后状况良好。2007年12月份，萨马兰奇参加由皇马守门员卡西利亚斯和西班牙网球明星纳达尔组织的慈善募捐活动时，突然短暂昏迷，之后就被送往了医院，两天之后病情好转终于出院。<br />
<br />
萨马兰奇于1966年获选为国际奥委会委员，1974年至1978年间担任副主席。1980年在苏联莫斯科举行的国际奥委会第83次全会上获选为国际奥委会主席，接替爱尔兰的迈克尔・莫里斯・基拉宁勋爵，一直在任至2001年退休。退休后担任国际奥委会终身名誉主席，及瑞士洛桑奥林匹克博物馆主席职务。<br />
<br />
<br />
-----------------------------------------------------------------------------------------------------------------------------------------------------<br />
前国际奥委会主席萨马兰奇于当地时间4月21日13点25分（北京时间19点25分），因心脏病抢救无效不幸逝世，享年89岁。<br />
他退休前的最大心愿，是希望奥运会能在中国举行。2001年7月，北京成功取得2008年夏季奥林匹克运动会的主办权，为他完成心愿。</div>

							
							
							
							
