　　以“三级贫户”的出身，站上了台湾最高权力的巅峰，陈水扁演绎了从草根到上层的命运大挪移。也许因为如此，当“台湾之子”逐渐变成“台湾之耻”，陈水扁将台湾政坛搅得天翻地覆的8年，也完成了自身一次又一次地蜕变。小编特意为读者献上一组精选图集，重温陈水扁从奋斗到沉沦的故事……<br />
