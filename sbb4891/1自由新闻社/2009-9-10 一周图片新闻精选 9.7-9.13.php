　　<strong>贫苦“赤脚”少年性格内向能吃苦</strong><br />
<br />
　　陈水扁出生在台南乡下一个世代贫困的农民家庭。父亲陈松根是佃农与长工。家里穷得常常要四处借债，写在墙上的欠债数字擦了又满。青黄不接时，常常不得不捡拾军人吃剩的饭菜果腹。父母没钱给他买鞋，每天上下学，他必须赤脚走1个多小时。大学时的陈水扁，竟然连冰箱和银行定期存单都没见过。<br />
<br />
　　小时候的陈水扁话很少，性格内向，非常强硬。他能吃苦，也非常刻苦。陈水扁上小学时，就懂得跟朋友借钱做小生意，为自己赚零花钱。过得再苦，他从来没有抱怨过，“人生没有选择”，他在日记里写。<br />
<br />
　　<strong>曾经的穷秀才与富家女</strong><br />
<br />
　　陈水扁和吴淑珍的爱情故事，曾经被誉为现代版的王宝钏与薛平贵。两人同为台南县人，又是中学校友，家境却有天壤之别。<br />
<br />
