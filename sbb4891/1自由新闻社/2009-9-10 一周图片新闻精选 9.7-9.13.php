　　编者按：陈水扁家族世纪弊案9月11日下午4时宣判，陈水扁将被提讯到庭聆听判决。一般预料，以陈水扁、吴淑珍夫妇贪污金额高达数亿元，陈水扁被判重刑的机率极高，但刑度是否如检方求处的“最严厉之刑”，在判决出炉之前，一切尚无法断言。<br />
