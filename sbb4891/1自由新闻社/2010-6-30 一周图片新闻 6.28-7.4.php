													<h2>中央党校首次接受外媒大规模采访</h2>
						
						
																											<div id="postmessage_717809" class="t_msgfont">.<br />
&nbsp; &nbsp; 2010年6月30日，来自国内外30余家新闻媒体的近70位记者来到北京中共中央党校进行集体采访。7月1日，中国共产党迎来建党89周年。 <br />
<br />
.<br />
<br />
<br />
<br />
<br />
.<br />
<br />
<br />
<br />
.<br />
<br />
