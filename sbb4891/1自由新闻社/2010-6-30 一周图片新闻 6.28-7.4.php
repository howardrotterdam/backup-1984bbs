6月30日，近70位中外媒体记者首次进入中央党校采访，其中境外记者人数达42人，占了大多数。为配合中共建党89周年，负责推动“党务公开”的中央外宣办与中央党校合作，组织了这次近年来规模最大的一次媒体采访活动。图为中央党校校门。<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://img3.cache.netease.com/photo/0001/2010-07-01/900x600_6AGOR2DR00AN0001.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
中外记者在中央党校采访，中央党校办公厅副主任张忠军表示，虽然此前已有国外媒体在中央党校采访过，但是大规模的采访，尚属首次。<br />
<br />
<br />
<br />
<br />
<br />
　　<img src="http://img3.cache.netease.com/photo/0001/2010-07-01/900x600_6AGOQGDI00AN0001.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
一名媒体记者在电子显示屏前拍摄。此次采访涉及观摩现场教学、采访教员和学员，以及进入中央党校体育馆、食堂、学员宿舍、礼堂，参观“实事求是”碑等。<br />
<br />
<br />
<br />
<br />
<br />
