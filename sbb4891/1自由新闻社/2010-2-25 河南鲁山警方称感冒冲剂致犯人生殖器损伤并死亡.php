看守所内嫌疑人“喝开水死亡”<br />
<br />
事发河南鲁山县，死者身上遍布伤痕乳头被割<br />
<br />
因涉嫌盗窃，2月18日，河南鲁山县一名叫王亚辉的男青年被公安机关带走，3天后其亲属被告知，王亚辉已在看守所内死亡。亲属查看尸体后发现，他身上有多处伤痕。对此，当地警方解释，犯罪嫌疑人是在提审时喝开水突然发病死亡的。<br />
<br />
伤痕满身乳头被割<br />
<br />
据河南电视台都市频道报道，王亚辉的舅妈吴女士介绍，2月18日，王亚辉被公安机关带走，就在家人不知道他犯了什么罪而焦急等待时，他们却突然接到公安机关通知，说王亚辉已在看守所死亡。2月22 日，家人在医院停尸房见到了王亚辉的尸体，脱掉他所穿衣服后，家人发现，他身上遍布伤痕。王亚辉的尸体照片显示，他的背部、手臂有大块淤青和伤痕，头部破了一个洞，乳头被割掉，生殖器也有伤痕。看到这种情景，王亚辉家人对王的死亡充满质疑。鲁山县公安局方面却表示，王亚辉是在提审时突然发病死亡的。<br />
<br />
吴女士说：“（公安机关告诉我们）他在看守所的时候是健康的，提审的时候也是健康的，审的时候肚子疼，突发疾病死了。”<br />
<br />
喝开水后突然死亡<br />
<br />
