													<h2>靠，凤凰是删节版；</h2>
						
						
																											<div id="postmessage_513946" class="t_msgfont">新华网北京３月１２日电 中国国务院新闻办公室１２日发表《２００９年美国的人权纪录》，以回应美国国务院１１日发表的《２００９年国别人权报告》对包括中国在内的世界１９０多个国家和地区的人权状况的指责。<br />
<br />
这是国务院新闻办公室针对美国一年一度的国别人权报告连续第１１年发表美国的人权纪录，目的在于“让世界人民了解真实的美国人权状况”。<br />
<br />
《２００９年美国的人权纪录》全文约１．３万字，包括“关于生命、财产和人身安全”、“关于公民权利和政治权利”、“关于经济、社会和文化权利”、“关于种族歧视”、“关于妇女、儿童权利”、“关于侵犯他国人权”等６个部分。<br />
<br />
人权纪录说，美国国务院１１日发表《２００９年国别人权报告》，再次以“世界人权法官”自居，对包括中国在内的世界１９０多个国家和地区的人权状况进行指责，而对自身十分糟糕的人权纪录熟视无睹、回避掩饰。<br />
<br />
人权纪录说，美国社会暴力犯罪严重，公民的生命、财产和人身安全缺乏应有的保障。美国私人拥有枪支数量居世界第一。校园成为暴力犯罪的重灾区。<br />
<br />
人权纪录说，美国政府限制、侵犯公民权利和政治权利的情况相当严重。美国执法人员滥用职权。监狱人满为患。囚犯基本权利得不到保障。<br />
<br />
美国一方面在世界上极力兜售“言论自由”、“新闻自由”、“互联网自由”，另一方面却完全按照美国自己的利益和需要，不择手段地监控、限制公民的自由权利。人权纪录说。<br />
<br />
人权纪录指出，美国的贫困、失业、无家可归者等问题严重，劳动者的经济、社会、文化权利得不到保障。美国失业率创２６年新高；贫困人口创１１年新高；挨饿人口居１４年来最高。劳动者权利受到严重侵害。没有医疗保险的人数连续８年增加。无家可归者激增。<br />
<br />
人权纪录说，种族歧视至今仍是美国社会的一大痼疾。黑人和其他少数族裔是最贫困的美国人。对少数民族的就业和职业歧视严重。少数民族在受教育方面受到歧视。执法和司法领域的种族歧视非常明显。“９・１１”事件以来，对穆斯林的歧视加剧。移民境遇悲惨。种族仇恨犯罪频发。<br />
<br />
人权纪录说，美国妇女儿童生存状况每况愈下，妇女儿童的权利得不到应有的保障。妇女不享有与男子平等的社会政治地位。妇女就业难、收入低、生活贫困。妇女频遭暴力和性侵害。美国儿童饥寒交迫。儿童生活在暴力和恐惧中。农业大量使用童工。美国是世界上唯一不对少年犯适用假释的国家。<br />
<br />
人权纪录说，美国凭借强大的军事实力，在国际上推行霸权主义，粗暴侵犯他国主权，肆意践踏他国人权。美国作为全球最大的军火销售国，加剧世界各地不稳定。美国的军费世界第一。伊拉克战争和阿富汗战争不仅给美国人民增加了沉重的负担，更给伊拉克和阿富汗两国人民的生命财产造成了巨大损失。虐囚是近年来美国在人权领域的最大丑闻之一。美国在世界各地设立军事基地，侵犯当地人民人权的事件屡见不鲜。美国对古巴进行长达近５０年的经济、商业和金融封锁，给古巴带来了超过９３０亿美元的直接经济损失。美国打着“互联网自由”的旗号，推行霸权主义。美国建立名为“梯队”的窃听系统，对全球进行窃听。美国漠视国际人权公约，消极对待国际人权义务。<br />
<br />
“美国不仅国内人权纪录十分糟糕，而且是世界许多人权灾难的主要根源。”人权纪录说。<br />
<br />
<br />
人权纪录说，长期以来，美国将自己凌驾于其他国家之上，充当“世界人权警察”，无视自身存在的严重的人权问题，年复一年地发表国别人权报告对别国进行指责，将人权作为干涉别国内政、丑化别国形象和谋取自己战略利益的政治工具，充分暴露了美国在人权问题上的双重标准，理所当然地遭到世界各国人民的坚决反对和强烈谴责。特别是在全世界人民正遭受由美国次贷危机引发的国际金融危机导致的严重人权灾难的时候，美国政府仍不正视自身存在的严重人权问题，而热衷于谴责别国，这是十分令人遗憾的。我们奉劝美国政府汲取历史教训，摆正自己的位置，着力改善自身人权状况，改正在人权领域的所作所为。<br />
