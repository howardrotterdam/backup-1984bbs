													<h2>中国驻日使馆官员首次探访冯正虎</h2>
						
						
																											<div id="postmessage_452518" class="t_msgfont"><a href="http://www.bbc.co.uk/zhongwen/si" target="_blank">http://www.bbc.co.uk/zhongwen/si</a> ... hina_activist.shtml<br />
<br />
<br />
童倩<br />
<br />
BBC中文网日本特约记者　发自东京<br />
<br />
滞留东京机场的上海维权人士冯正虎称，中国驻日大使馆官员首次到机场对他进行探望。<br />
<br />
上海维权人士冯正虎星期二（1月26日）对BBC中文网表示，中国驻日大使馆一等秘书杜先生和另一名兼领事的赵先生星期一上午到成田机场探访了他。<br />
<br />
这成为冯正虎去年11月3日第八次回国不成、被迫流落东京成田机场83天后，中国官方首次有人与他会面的记录。<br />
<br />
此前，中国大使馆1月23日曾“召见”冯正虎在日本定居的妹妹，表示刚获悉冯正虎的遭遇，要她转告冯正虎“有问题应向上面汇报，不要被人利用”等。<br />
<br />
上海公安当局几乎同时也找冯正虎在上海的家人说，冯正虎应与中国大使馆沟通。<br />
<br />
对此，冯正虎在网络上公开说，他有手机等各种联络工具，所以任何带话或传话，他都不当真。中国政府官员来机场见他是对公民诚意的问题。<br />
<br />
BBC中文网记者星期二打电话给中国大使馆求证时，日本外务省向外国记者颁发的各国使馆电话号码表中的中国使馆号码，到中午12点为止无人接听、大使馆总机录音说明的询问内线号码也到中午仍无人接听。<br />
<br />
领事部的电话在上午11点54分接通时，电话录音介绍了各项服务内线，但也没有人应对的窗口。<br />
传话不成亲自登场<br />
<br />
两天后，大使馆官员去了成田机场。<br />
<br />
中国官员星期一对冯正虎说，他们是来探望他、关心他和考虑他回国的问题，成田机场不是正常的地方，他们进来也不容易，不过感谢冯正虎信任他们、欢迎他们来，所以他们就来了。<br />
<br />
冯正虎说，他去年6月7日第一次回国被拒后，他就到中国大使馆递交申诉书，大使馆不理；第一次在成田机场被航空公司拒绝他登机回国，他当场又打电话给大使馆，大使馆表示这是他与民间公司之间的纠纷，与他们无关。<br />
<br />
后来冯正虎不断写信给中国国家主席胡锦涛、总理温家宝和全国人大委员长吴邦国，以及访日的国家副主席习近平申诉回国、回家，他不懂怎样才算是“向上面汇报”。<br />
耐心等待追究责任<br />
<br />
冯正虎对使馆官员来访表示感谢。他说，虽然自己国家的官员是在他流落外国80天后才来，比日方来慰问还迟，但中国官员总算是尽了责、面对了现实。<br />
<br />
冯正虎说，约半小时会面中，中国官员先看了他在成田机场的生活空间，包括那些申诉回国、回家和中国人权的标语牌，以及睡觉的长凳下摆满的各国旅客捐赠的食品等。<br />
<br />
冯正虎说，他感觉到使馆官员也希望他回国，但目的与他并不一致。<br />
<br />
冯正虎说，回国问题没什么好谈判的，公民有回国权，只要国门开着就行。但他要追究把他堵在国门外，令他饱受困苦的上海当局，至少下令禁止他回国的人要以某种方式道歉。<br />
<br />
冯正虎对大使馆官员说，他不期待与他们沟通一两次就能解决问题，他有耐心等他们回去慢慢与各方沟通、纠正上海官员违法的错误。<br />
<br />
冯正虎说，对他而言，流落成田机场80天和800天是一样的，等了80天才与中国官员对上话，等800天才能解决问题也不出奇。</div>

							
							
							
							
