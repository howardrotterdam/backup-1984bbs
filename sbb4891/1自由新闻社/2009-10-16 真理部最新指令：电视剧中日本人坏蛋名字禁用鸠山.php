　　日本民间流传一件趣闻，讲1976年日本代表团到北京访问，外相鸠山威一郎的长子鸠山由纪夫曾向中国总理周恩来提出了一个“私人”话题，称中国京剧《红灯记》里那个一号侵华日军头目为什么叫“鸠山”呢。在日本姓“鸠山”的不多。他查过了，没有叫鸠山的在中国干过坏事。鸠山请周总理帮忙还鸠山家一个清白。此说不见于中文记载，但不少日本人都相信这传闻。这是因为，鸠山家是望族，他们家的人到前线服役的可能性不大，而且鸠山家与中国有着颇为密切的关系。<br />
<br />
　　鸠山和夫 调停与北洋水兵的冲突<br />
<br />
　　在鸠山家族中，出任高官的第一人，是1896年任众议院议长的鸠山和夫。鸠山和夫是日本首批公派赴美留学生，毕业回国后任外务省调查局局长和翻译局长，专门处理涉“洋”事务。1886年8月，中国北洋舰队访问长崎期间，中国水兵与日本警民发生冲突双方多人死伤。处理这事的，正是鸠山和夫。当时，北洋水师总教官甚至建议舰队司令官炮击长崎。鸠山等与中方交涉，最终以日本赔偿损失了结。事后，清政府颁给鸠山和夫二等龙宝勋章。<br />
<br />
　　鸠山一郎 与张学良有个秘密交易<br />
<br />
　　鸠山家的第二代掌门人，是曾三度组阁并出任首相的鸠山一郎。他1954年组建了日本民主党。1926年，鸠山一郎为日本政友会干事长。1931年9月18日，关东军攻入张学良官邸，日军打开了张学良卧室保险柜，发现里面有张资助日本政友会政客的证据，仅一次送床次就达50万元。据日方披露，张与政友会有密约――由张资助床次竞选首相，而政友会则承诺“掌权后，逐步放缓并最终结束日本对东北的侵略进程”。政友会政治家们接受张“政治献金”期间，鸠山一郎正担任该党总务部长。<br />
<br />
　　鸠山威一郎负责《中日和平友好条约》谈判<br />
<br />
　　鸠山家的第三代人物，是鸠山威一郎。鸠山威一郎1970年当选参议员，曾多次访华。1976年，鸠山威一郎担任外相，具体负责与中国就缔结《中日和平友好条约》进行谈判。1954年，鸠山家族建立了日本友爱青年协会，1974年，鸠山威一郎接替其母薰夫人就任日本友爱青年协会会长。同年，该协会与中国中日友好协会建立合作关系，并于1980年，先后派出三期“中日友好亲善使节团”访华，也曾接待中国青年代表团访日。<br />
<br />
　　鸠山由纪夫“任首相后不拜靖国神社”<br />
<br />
　　鸠山由纪夫和弟弟鸠山邦夫，是鸠山家族的第四代领军人物。鸠山由纪夫是日中友好协会副会长，提倡“友爱外交”。其夫人鸠山幸生于上海，也与中国有不解之缘。鸠山由纪夫多次率团访华，还多次反驳“中国威胁论”。日右翼曾声称：“鸠山由纪夫不是亲中派，而是‘媚中派’”。<br />
<br />
　　鸠山由纪夫的弟弟鸠山邦夫，1980年曾任“东京都日中友好协会”副会长，现任“日中友好议员联盟”理事。<br />
<br />
　　（来源：华商报）</div>

							
							
							
							
