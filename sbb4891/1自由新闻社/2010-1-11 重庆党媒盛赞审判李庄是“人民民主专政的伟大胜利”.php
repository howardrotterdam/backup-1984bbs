																											<div id="postmessage_425373" class="t_msgfont">秋风，萧瀚等致李庄相关司法人员的公开信<br />
致李庄案相关司法人员的公开信<br />
<br />
秋风，萧瀚，王建勋，郭玉闪<br />
<br />
<br />
承担司法之职而践踏正义者，是正义的最大罪人。<br />
<br />
2010年1月8日，以付鸣剑为审判长的重庆市江北区法院合议庭，对引人注目的李庄案作出一审判决：“被告人李庄犯辩护人伪造证据、妨害作证罪，判处有期徒刑二年六个月。”在当下重庆浓厚的政治运动气氛中，这样的判决结果似乎并不出人意料。但是，这样的判决已经践踏了法治的基本原则，而这个法庭之外、之上的“法治的高级法庭”已经对这个法庭做出了有罪的裁定。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 李庄案件的不少重要证据，尤其是公安机关获得证据的方式，已为公众周知。任何一个熟知法律程序、尤其是证据规则的法律人，哪怕是具有正常理智的公众，都无法不对如此获取的证据的可信性表示严重质疑，甚至对逮捕及起诉李庄的合理性表示怀疑。令人不可思议的是，判定李庄有罪的证据主要是一些证人的证言，而这些证言不仅是在证人失去人身自由的情况下作出的，而且是在没有一个证人出庭质证的情况下被采信的。这样的证人证言具有证明力吗？江北区法院能够依据这些证人证言定案吗？ <br />
<br />
但是，分别从重庆市检察院第一分院和第五分院空降到下级检察院担任公诉人的贺贝贝、么宁两位检察官，对公安机关如此获得证据没有进行必要的审查，而在极短时间内，匆忙将其提交法庭。在法庭上，两位检察官对被告及其辩护人提出的关键问题和质疑拒绝作出回答。在庭审最后阶段，么宁检察官更是拿出道德审判的杀手锏，指控李庄曾经嫖娼，却未出示任何证据。<br />
面对如此蛮横、拒绝对自己提交的证据承担责任的公诉人，由重庆江北区法院付鸣剑等法官组成的合议庭，未对其采取有效措施，使其满足辩护人提出的对关键证据进行质证的要求。相反，在整个庭审过程中，审判长密切配合检察官，对被告一方明显不公。<br />
<br />
这样的审判过程，透过媒体的报道，已经为法律人共同体乃至整个社会所知晓。重庆方面在最初逮捕李庄后，通过不良媒体的宣传，成功地摸黑了被告。但随着其它媒体负责任的报道，尤其是关于庭审过程的报道，通过阅读陈有西律师的辩护词、李庄的自辩词，无数人已经改变了对李庄的看法。众多法律界人士和普通民众无不怀疑，李庄的罪名能否成立，本案是否系重庆警方旨在遮掩其系统性违法活动、刻意打击律师而人为制造的案件。令人遗憾的是，坐在公诉人席上的两位检察官，却不遗余力地为这些证据进行强词夺理式地辩护，而法官最终也依据这些常人均无法认可的证据，做出有罪判决。<br />
<br />
我们不能相信，这几位检察官和法官是在心安理得地制造这起冤案。检索这几位检察官、法官的履历，他们都接受过正规的法律教育。么宁检察官正在攻读博士学位，她结合办案体会撰写过调研文章《死刑二审法律监督问题研究》、《建立行政公益诉讼制度推进和谐重庆建设》、专著《死刑案件法律监督理论与实务》(中国检察出版社出版)。付鸣剑法官也曾于2000年在《现代法学》上发表论文《完善我国刑事证人作证制度的立法构想》。<br />
<br />
依据这些法律学术活动，我们相信，你们，贺贝贝、么宁两位检察官和付鸣剑法官是知道法治精义的，具有起码的法治信念，你们知道程序正义对于司法公正的决定性意义。但你们在司法操作过程中背叛了自己的信念，背叛了法治的精神，不顾基本程序正义，使用了自己论著中明确反对的操作手法。<br />
<br />
我们知道，你们有不得已的苦衷。从唱所谓“红歌”、发所谓“红色”短信开始，重庆就已陷入小型文革式狂热；在这种社会气氛中进行的重庆打黑，从一开始就是一场超越法律框架的政治运动，就是“黑打”，司法也被当成随意玩弄的工具。我们相信，你们未必从一开始就愿意充当掌握强权者的工具，只是面对过于强大的政治压力，面对个人生计可能遭遇的巨大风险，你们选择了屈服。<br />
<br />
当然，在这场反法治的运动中，并不只是你们背叛了法治的信念。就职于西南政法大学的陈忠林、高一飞、潘金贵、李昌林、梅传强等诸位法学教授，也匆忙而积极地为重庆公安的做法、为检察官的证据和法院的程序进行辩护。<br />
这些检察官、法官、法学教授们，你们对法律已经犯下了罪，法律人共同体和公众会记住这一切。<br />
<br />
对于接下来的上诉审的公正性，全国民众，包括我们自己，都在拭目以待。我们真诚地希望，承办这一案件的检察官和法官，能够在政治压力之下，认真而严肃地考虑一下法律的尊严，从而有资格在不远的将来，当真相大白后对法律人共同体和举国公众说，当时我已经尽了自己的力量来守护法律的尊严。如果你们没有进行这样的努力，那你们也将成为法律的罪人。<br />
我们也想对那些正在滥用强权操纵法律的人士说，通过政治运动的方式进行“黑打”、操纵司法打击为被告辩护的律师，最终必将遭到政治或者司法的超常规惩罚。那个时候，你们也会期待一个负责任的律师为自己辩护。历史上，那些滥用不受节制的权力者，最终也都难免遭受这样的权力之祸，到时候你们必将投告无门。<br />
<br />
我们对你们这些法律的罪人遭到法律及舆论惩罚的前景充满信心。因为，哪怕是观察李庄案件的整个过程，我们也对中国的法治前景保持一定信心。<br />
<br />
确实，今天，你们利用强权或慑于强权而能够肆无忌惮地扭曲法律、强奸法律，但你们想必也已经看到，另一个更高级的法庭，同时在江北区法院、在重庆之外开庭了：接受审判的是那些罔顾新闻底线发表抹黑李庄和律师制度的不良记者与媒体，有滥用权力拼凑证据、伪造证据的公安部门，有失职的检方和审判人员，以及所有这些违反正义的司法活动背后的政治力量。起诉人则是坚守法治信念的法律人共同体，包括赵长青教授，包括李庄案的两位辩护人陈有西律师和高子程律师，包括那些积极地运用法律分析案情、证据可信性和程序的法学家，以及坚守良知的评论家。甚至也包括李庄本人，他虽然知道重庆正在进行一场政治运动，仍然承接案件，在法庭上也依据法律积极地维护自己的权利。坐在审判席上的是具有朴素正义感的人民，尤其是那些积极聆听、理性判断的网民。<br />
<br />
这两个法庭同时开庭，而这个真正由人民组成的法庭通过聆讯，已经依据高级法，依据法治的原则和精神，已经先对李庄案作出了裁定，进而对以付鸣剑为审判长的合议庭的判决做出了裁定。这两个裁判已铭刻在法律人共同体和人民的心灵中，任何人也抹除不了。在今日中国，任何从事司法活动的人、掌握权力的人，都不应忽视这个法庭的存在及其裁决的效力。<br />
而这个高级法庭的运作，乃是中国历史出现转机的关键所在。中国历史长期在治乱循环中徘徊，不要说那些掌握权力者，即便是那些被强权压迫和剥夺者，也总是迷信强权，误把强权当救星。但今天，经历过无数灾难而接受了法治理念的中国人，已经越来越多地树立了这样一种信念：只有法治能够救中国，只有把权力置于法律控制之下，每个人才能够免受强权之苦。法治不仅能保护弱者，也能拯救那些拥有权力的貌似的强者。<br />
<br />
正是基于这样的信念，“法治的高级法庭”在法律人共同体、维权公民、公共知识分子、甚至众多官员的支撑下，逐渐地成形并有效地运转。它在每一起强权侵害弱者的公共事件中开庭，在每一起引人瞩目的公民维权活动中开庭。在这样的公共对话、论辩过程中，正义的法律的力量开始涌出压制强权，人的理性的力量逐渐浮现抗衡强力。如果说不受节制的权力和甘当这种权力之工具的法律工作者们在拖动中国社会向下继续堕落的话，那法治的信念、法治的高级法庭的运作，就是构建法治秩序的向上的力量。<br />
<br />
这就是中国历史演进的大趋势。面对这样的趋势，每一个司法从业人员都应当做出明智的选择。梅传强教授通过媒体辨明，自己当初没有无条件支持本案检方、法院的做法。这就表明，法治的良知即便被权力、财富压制到心灵最晦暗的地方，也依然能顽强地伸张自己。据此，我们仍然相信，法治的良知、哪怕一个正常人的良知，在即将接受李庄上诉案的检察官、法官的心灵中，不可能完全泯灭。<br />
<br />
这种法治的良知，是法治的高级法庭的裁决依据，也应当是现实的法庭的裁决依据。我们希望，所有法律人，不论是律师、法官，还是法学教授，面向这种良知，把自己变成正义的仆人，用法律驱逐强权，推动公义早日降临华夏大地。<br />
<br />
二○一○年一月十一日</div>

							
							
							
							
