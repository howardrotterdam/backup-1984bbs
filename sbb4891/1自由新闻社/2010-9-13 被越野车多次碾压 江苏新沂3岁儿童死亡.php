													<h2>被越野车多次碾压 江苏新沂3岁儿童死亡</h2>
						
						
																											<div id="postmessage_827547" class="t_msgfont">事发新沂一小区，肇事司机目前已被刑拘<br />
7日上午，在新沂市青年路良辰花苑小区，三岁半男童乐乐（化名）正在小区内玩耍，一辆宝马X6突然倒车撞倒他，车左后轮从乐乐上半身碾过。令人惊愕的是，宝马车紧接着又反复三次碾轧乐乐，导致其惨死。昨天，男童的父亲李先生强忍着悲痛，向记者诉说了发生在他儿子身上的这幕惨剧，希望法律能够严惩肇事司机。<br />
惨剧：宝马反复四次碾轧男童<br />
李先生告诉记者，他家住良辰花苑小区9号楼，7日上午10点50分左右，儿子乐乐有点饿了，于是吵着要奶奶带他到楼下商店买面包吃。买过面包后，乐乐就在小区内9号楼和10号楼中间的水泥道路上边吃边玩耍。<br />
上午11点钟左右，一辆宝马X6停在10号楼三单元前面，两名男子把车里的东西拿出来并送到楼上。两分钟后，好奇的乐乐跑到宝马轿车后面玩耍。这时，司机没有观察车后情况，直接上车就把车子往后倒，距离车后一两米的乐乐，被宝马车左后轮轧住，当时车子停顿了一下，突然又加油门从乐乐身上碾轧了过去。令人震惊的是，车子没有直接倒过去，司机这时又把车子朝前开，车左后轮第二次从乐乐身上轧了过去。司机这时才停车，司机下车后，无人驾驶的宝马车竟自己又往后倒了回去，车左后轮再次从乐乐身上碾轧过去，由于没停下来，紧接着车子左前轮也从乐乐身上碾轧过去。<br />
“就这样，我儿子被那辆宝马车反复碾轧了四次！”说完这些，痛不欲生的李先生，蹲在了地上，双手捂着脸啜泣着，泪水从他指缝中流了出来。<br />
家长：司机肯定想“碾伤不如碾死”<br />
为了弄清楚儿子是怎么死的，李先生和家人看过多遍录像，录像上的每一分一秒，就像刀子在他们的心上刮来刮去，李先生的母亲和妻子数度哭昏在地。<br />
记者仔细地看了几遍李先生提供的录像资料，5分钟的现场录像，每看一遍都是一次痛苦的历程。<br />
影像资料显示：当日中午11：03：18，宝马车撞倒乐乐；11：03：20，乐乐的上身左半侧被轧在车轮下，双腿在车底直蹬，宝马车停顿了一下；11：03:30，宝马车左后轮从乐乐身上碾过；11：03：33,司机没有继续倒车，反倒把车子往前开，第二次从乐乐身上轧过；11：03：38，宝马车停住，司机下来，不知为何空车自己又继续往后倒了，左后轮第三次从乐乐身上轧过；11：03:44,车子左前轮从乐乐身上轧过去，乐乐总共被碾轧了四次。此后，司机观察了一下血泊中的乐乐，未采取任何措施，随即离开现场。<br />
李先生气愤地说：“宝马司机这是在故意杀人。”乐乐有1.16米高，该宝马车既有后视镜，又有倒车雷达影像，两者只要观察一样，都能及时发现乐乐的。司机在第一次将乐乐轧到车底乐乐还在挣扎时，司机就应该下车检查轧到什么异物了。但司机没有，只是停顿了一下后，左后轮直接碾轧了过去。此后，司机竟朝前挂挡再次碾轧过去。这时车已经停下了，司机也下车了，车子怎么会自己又朝后倒呢？于是又有了左后轮、左前轮的连续两次碾轧。并且，下车后确认孩子已经死亡后，司机既没打电话报警，也没采取任何抢救措施就离开了。因此，综上所述，他认为司机肯定有“碾伤不如碾死的想法”。<br />
警方：肇事司机已被刑拘<br />
据新沂交警大队事故中队的一位领导介绍说，事发当日，办案民警赶到现场，将仍留在小区内的肇事司机伍某控制住后，又将肇事车辆进行了暂扣。<br />
事发现场是平整的水泥路面，且视线良好。通过调取良辰花苑小区监控录像发现，伍某驾驶苏CLH×××越野型宝马X6轿车，的确有反复碾轧男童的画面。因此，办案民警处理的非常慎重，并将此事向上级汇报。后来又对肇事司机进行血液抽查，发现其并不是酒驾。通过对肇事车辆进行检验、鉴定和评估，发现该车后视镜及倒车雷达影像均有效。<br />
根据当事人的陈述、现场勘查、相关证人证言、鉴定结论等证据证实：当事人伍某驾驶机动车倒车时未察明车后情况，确认安全后倒车，是造成事故的直接原因。根据《中华人民共和国道路交通安全法实施条例》第50条规定：机动车倒车时，应当察明车后情况，确认安全后倒车。根据《道路交通安全实施条例》第91条、《道路交通事故处理程序规定》第46条，当事人伍某应承担事故全部责任，当事人李先生的儿子无责。<br />
昨天，当记者将李先生的想法告诉新沂市交警大队相关领导时，该领导解释说，他非常理解李先生的想法，但这只是李先生的个人猜测。对宝马车反复碾轧男童致死画面的出现，他分析说可能有两种情况。一种情况就是因为伍某和李先生及其亲友有很深的过节，伍某故意为之；另外一种情况就是事发后伍某惊慌失措所致。经过调查，伍、李二人之间均无任何过节，因此伍某也就不存在“故意杀人”的动机。目前，警方已经启动刑事程序，伍某因涉嫌交通肇事罪被刑拘。这位领导表示，如果李先生有想法，可通过找律师走法律程序。</div>

							
							
							
							
