成报 <br />
A12 |&nbsp;&nbsp;两岸新闻&nbsp;&nbsp;2009-04-16<br />
<br />
粤政协主席陈绍基涉贪「双规」&nbsp;&nbsp;<br />
<br />
【本报综合报道】前广东省公安厅厅长，广东省政协主席陈绍基被传因涉嫌经济问题，在上周日被「双规」。内地网站21cn.com的BBS论坛贴出该留言后很快被删除，有消息指曾任陈绍基的副手，原广东省纪委副书记王华元亦在同一时间被查，两人均可能涉及曾任广东省公安厅刑侦大队长的公安部部长助理兼经侦局长郑少东案件。消息人士透露，陈绍基是在广州由中纪委人员带往北京。他抵达北京后，中纪委正式宣布对他实施「双规」（在规定时间、地点交代问题）。 <br />
<br />
　　内地21cn的bbs论坛「社会热点」区，前日出现「现广东政协主席、前公安厅长陈绍基被双规」的留言。但有关文章很快被删除。据报道，现年63岁的陈绍基为广东中山人，从70年代一直在广东省公安系统工作，1991年升任公安厅厅长，在2004年担任广东省政协主席，去年连任。 <br />
<br />
传原纪委副书记王华元同被查 <br />
<br />
　　据消息指，陈绍基是在上周日下午被实施双规的，目前正接受有关方面的调查。陈绍基被查，可能与较早前曾任广东省公安厅刑侦大队长的公安部部长助理兼经侦局长郑少东被拘查有关。该两人当年曾侦破张子强一案而名噪一时。在今年1月，郑少东因涉及国美主席黄光裕案而被「双规」审查，郑少东曾长期在陈绍基手下工作。 <br />
<br />
　　消息更指，同时被查的还有任陈绍基副手的原广东省纪委副书记王华元，目前王华元任浙江省纪委书记，陈绍基和王华元以及郑少东均是当年广东政法系统的重量级人物。有关的消息最早在4月12日晚间即在广东当地高层人物间流传。但政协广东省委员会办公厅的网站上，对陈绍基职衔的表述仍显示为主席，分管工作范围为主持政协全面工作，未见有变动。广东省有关政府部门的人员对该消息表示「是真的」，对其真正被双规原因，则不予透露。<br />
