Guangdong cases uncover years of abuse, sources say&nbsp;&nbsp;<br />
 <br />
The recent falls from grace of senior officials in Guangdong's judicial, public security and prosecutorial systems have uncovered rampant corruption that has been plaguing the province for years, sources say. <br />
<br />
The shuanggui order - a form of Communist Party discipline - issued on Sunday to Chen Shaoji, the highest-level Guangdong figure to be implicated in the scandal, came as no surprise to those working in the province's legal system. <br />
<br />
We had already seen several senior officials who once worked in Guangdong's judicial and public security agencies arrested in the past six months, like Zheng Shaodong, a source close to the public security bureau said. <br />
<br />
In January, Mr Zheng, the former assistant minister of public security and director of the ministry's economic criminal investigation bureau, was detained by the Central Commission for Discipline Inspection. Mr Zheng is also under shuanggui. <br />
<br />
Besides Chen Shaoji, there might be more high-ranking officials from Guangdong, especially Guangzhou, under shuanggui. Don't be shocked if that news comes out in the future, the source said. I had heard Beijing would take action against some top officials soon since the corruption and clique problems were serious in Guangdong's legal, judicial and law enforcement systems. <br />
<br />
Another source said Mr Chen and Mr Zheng were close to Wang Huayuan, secretary of the Discipline Inspection Commission in Zhejiang province, who was also placed under shuanggui on Sunday. <br />
<br />
Mr Wang was once in charge of internal party discipline and anti- corruption affairs in Guangdong, and Mr Chen was his supervisor as deputy party head, and head of Guangdong's police and judiciary. <br />
<br />
In 1998, the three men built their reputations together in an operation that led to the arrest of gangster Big Spender Cheung Tze-keung, who had kidnapped Hong Kong tycoons Walter Kwok Ping-sheung and Victor Li Tzar-kuoi for ransom. <br />
<br />
After the famous case, the gangbusters became rising political stars. Mr Chen was promoted to deputy party head and Mr Wang to head of Guangdong's Discipline Inspection Commission. <br />
<br />
On Sunday, the two old friends went from hero to zero, but their cases were not the first of their kind in Guangdong. <br />
<br />
In October, Yang Xiancai, the former head of the Guangdong Higher People's Court, was placed under shuanggui and sent to Beijing for being linked to corruption. <br />
<br />
<br />
Also in October, the National People's Congress confirmed the downfall of another senior official at its Standing Committee meeting. Huang Songyou, vice-president of the Supreme People's Court, was put under investigation for alleged abuses of power. Mr Huang had worked in the Guangdong Provincial Higher People's Court for years. <br />
