A21 |&nbsp;&nbsp;中国&nbsp;&nbsp;2009-04-16<br />
广东政协主席陈绍基双亲 原纪委书记王华元亦涉案&nbsp;&nbsp;<br />
 <br />
广东政坛出现大震荡。消息透露，广东省政协主席陈绍基和原广东省委副书记、纪委书记王华元，近日同时被中央纪委「双规」(在规定时间和地点交代问题)。据悉，作为广东政法大佬，陈绍基「出事」与国美电器黄光裕、公安部部长助理郑少东案有关。 <br />
<br />
本报记者 <br />
<br />
有香港的广东省政协委员前天已经收到陈绍基「出事」的消息。据悉，陈绍基是和现任浙江省委常委、纪委书记王华元同时被带走的。本报昨天致电广东省政协值班室查询，一名官员称「不了解」；记者追问「那陈主席双规的消息是造谣吗」，他依然称「不了解」。 <br />
