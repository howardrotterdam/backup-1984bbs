本报昨天报道，陈绍基上星期日被中纪委“双规”，成为广东30年来首名被“双规”的正省级高官，而王华元星期一凌晨也在杭州的住处被中纪委人员带走，成为中国首名被“双规”的省纪委书记。多家香港媒体报道，陈绍基和王华元被“双规”与黄光裕案件有密切关系。<br />
<br />
对于广东近日爆发的政坛震荡，有政治分析人士认为，多名广东高官落马显示广东省委书记汪洋要击破广东地方派系、坚持改革道路的决心，广东高官落马不但是反贪的胜利，也证明汪洋在广东的强硬改革政策其实是得到中央支持的。<br />
<br />
深圳学者朱健国接受《南华早报》访问时说，来自外省的汪洋和广东派系的官员一直都有内部斗争，而近日的事件显示，汪洋胜利了。据他所知，很多广东地方派系的官员不赞同汪洋铲除劳动密集型产业的政策，毕竟劳动密集型产业过去对广东的贡献良多。他也指出，在今年3月的全国两会期间，很多与企业关系较密切的广东政治顾问批评了汪洋铲除劳动密集型产业的政策。<br />
<br />
另一名政治分析人士则说，要陈绍基这样在地方上有权势的高官落马，并非汪洋一人的能力所能为，背后一定要有中央的支持。而汪洋是中央派来管理广东的，因此中央务必给他全力的支持。<br />
<br />
63岁的陈绍基是广东中山人，毕业于中山大学中文系，长期在广东公安系统工作，90年代开始历任省公安厅厅长、省政法委书记、省委副书记、省政协主席，他在广东的人脉非常广，无论政界或商界都很有影响力。学者：表现反贪决心<br />
<br />
