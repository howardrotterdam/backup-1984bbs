<br />
<br />
广东政坛大地震！消息人士指出，广东政协主席陈绍基因经济问题，周日（12日）深夜被双规（在规定时间规定地点交代问题），同日被双规的还有前广东省副书记、现任浙江省纪委书记王华元。消息指，陈绍基「出事」，与国美电器主席黄光裕及公安部部长助理郑少东被查有关。陈绍基是广东首名被双规的正省级官员，广东当局已召开会议，传达陈被查一事，要求官员们加强廉洁自律。 <br />
<br />
