广东政协主席陈绍基和浙江纪委书记王华元“双规”的消息<br />
星岛日报 <br />
A21 |&nbsp;&nbsp;中国&nbsp;&nbsp;2009-04-16<br />
广东政协主席陈绍基双亲 原纪委书记王华元亦涉案&nbsp;&nbsp;<br />
<br />
广东政坛出现大震荡。消息透露，广东省政协主席陈绍基和原广东省委副书记、纪委书记王华元，近日同时被中央纪委「双规」(在规定时间和地点交代问题)。据悉，作为广东政法大佬，陈绍基「出事」与国美电器黄光裕、公安部部长助理郑少东案有关。 <br />
<br />
本报记者 <br />
<br />
有香港的广东省政协委员前天已经收到陈绍基「出事」的消息。据悉，陈绍基是和现任浙江省委常委、纪委书记王华元同时被带走的。本报昨天致电广东省政协值班室查询，一名官员称「不了解」；记者追问「那陈主席双规的消息是造谣吗」，他依然称「不了解」。 <br />
<br />
港商被捕供出多名高官 <br />
<br />
消息透露，早在○七年九月，广东省政协常委、港商吴汉良因为涉嫌诈骗被拘捕，就传出招供出多名高官，其中包括陈绍基和文化部党组书记于幼军。「吴汉良能当上港区广东政协委员联谊会召集人，当然是陈绍基钦点的。」据悉，当时中纪委已经开始注意陈绍基。去年国美电器主席黄光裕落马，公安部长助理郑少东也涉案被捕，中纪委开始找陈「谈话」。 <br />
<br />
黄光裕出事中纪委找谈话 <br />
<br />
郑少东是陈绍基一手培养的嫡系爱将。陈绍基担任广东省政法委书记、公安厅厅长期间，郑少东在其手下从事刑事侦察工作，两人当年联手侦破张子强绑架香港富商案而蜚声粤港澳。消息透露，黄光裕、郑少东以及「公海赌王」、港澳赌业重要人物连超被拘之后，供出陈绍基，导致这位南粤「政法王」落马。 <br />
<br />
六十三岁的陈绍基，广东中山人，毕业于中山大学中文系，长期在广东公安系统工作，九十年代开始历任省公安厅厅长、省政法委书记、省委副书记、省政协主席。他也是广东首位落马的正部级官员。 <br />
<br />
至于同时被「双规」的王华元，现年六十岁，安徽人，空军出身，官至广东省委副书记兼纪委书记。二○○六年，曾传出其包养二奶并且育有私生子的消息，他转任浙江省委常委、纪委书记。 <br />
<br />
王华元曾说：「包二奶」现象，作为党员干部是绝不允许的，我们要发现一宗查处一宗，绝不姑息。他是中共十五大、十六大、十七大中央纪委委员，相信是首位因为涉嫌经济问题落马的省级纪委书记。<br />
