<br />
　　广东省委一名官员透露说，4月14日下午15时，广东在正厅以上级别官员范围内，对此事进行了通报。省委主要领导在通报会议上表示：最近会有很多不利于广东的声音出现。要努力树立广东干部的良好形象。<br />
<br />
