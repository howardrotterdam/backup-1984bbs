<br />
另外，继深圳纪委一二把手被“双规”后，一些政治分析人士担心，广东的政坛震荡若一直持续下去，将可能影响广东在环球经济危机大环境下的改革步伐。他们也认为，广东的反贪风暴可能还会持续一段日子，而最终被牵连的可能不止纪委官员，其他部门的官员也随时有被“双规”的可能性。<br />
