																											<div id="postmessage_234435" class="t_msgfont">维吾尔在线网站创办人伊力哈木谈近日新疆局势 <br />
Source: 德国之声 2009.09.09 <br />
<br />
上周，新疆首府乌鲁木齐发生大规模抗议示威事件。中国官方证实抗议活动中有五人丧生，但是没有透露死者的身份。上周六，中国媒体报道称，乌鲁木齐市委书记栗智遭撤职，新疆自治区公安厅主要负责人事也做出了调整。本台记者就新近发生的乌鲁木齐抗议事件采访了维吾尔在线网站创办人伊力哈木。今年新疆”7.5″ 事件发生之后不久，伊力哈木突然下落不明。8月23日他重新恢复自由。<br />
<br />
德国之声：9月初，乌鲁木齐地区发生了万人参与的大规模抗议示威活动。当地市民要求当局为市民生活提供更多安全保障。您所了解到的抗议事件是怎样一种情况？<br />
<br />
伊力哈木：这个应该是维吾尔网站最先透露出来的消息，自由亚洲维语网页也做了报道，当时他们采访了卫生厅的一个干部。据说是在乌鲁木齐民汉合校的实验中学，这是新疆最好的学校，打疫苗针，不知道哪个部门组织打的，谁打的，但对维族学生打。报道说，有15个学生之后死亡，几百人住进新疆人民医院。这是自由亚洲的报道。我们有一些亲戚在当地，他们也感到恐慌。过了几天就有汉族网友在网上称，在新疆有维族人针刺（行凶）事件。<br />
<br />
德国之声：被要求打疫苗针的都是维吾尔学生吗？<br />
<br />
伊力哈木：学校是民汉合校，但是被打疫苗针的是维吾尔族。<br />
<br />
德国之声：死亡学生的死因有没有查清楚？伊力哈木：这个不清楚，因为没有官方的说法。这件事是自由亚洲报道的，但是我没办法确认这个信息的真假。<br />
<br />
德国之声：这个事情怎么和针刺事件联系到一起的呢？<br />
<br />
伊力哈木：我注意到汉族同胞、汉族游行者的诉求。除了治安之外，就是要王震。大家都知道，王震对维吾尔族来说是什么样的人。我不知道汉族社会怎么看。他们要的不是民主的诉求，他们要的不过是C觉得现在杀的人还不够吧？或者说镇压得不够，要再强硬一点。我支持民众提出的安全诉求，包括人身的安全、家庭的安全，包括自己的工作环境，公民权利方方面面的东西。但是这种要求强硬镇压的诉求和维族的诉求根本是不一样的。而且在新疆一直有严厉的措施，再严厉的话，会像什么地方？像当年的南非一样？<br />
<br />
德国之声：那么之前媒体一直提到的针刺事件究竟存在不存在？<br />
<br />
伊力哈木：我对针刺事件的真实性表示怀疑。现在虽然说嫌疑犯抓了一些人，刑拘了4个人，但是具体怎么回事，不知道。还有一个情况就是，维族当中有一些吸毒的人，”7.5″之后他们纷纷回到新疆。其中很多人确确实实在内地是从事犯罪活动的。有报道说，他们（犯毒瘾的时候）实在受不了了，就给自己打（针），但最后被人打死。现在具体情况到底是怎么回事，这个不清楚。<strong>但是有一点，很多维族人，包括在网上或者打电话都说，”7.5″之后7月6日，7月7日发生了什么，维族人开的店被砸，清真寺被冲击，维族人被打伤打死，都没有报道。在戒严的情况下，汉族可以不经批准就上街，明显对维族的态度就不一样，这是维族的看法。从新闻来看我也认为有差距。每天有死亡的事件，被伤害的事件发生，但是官方一直否认。到现在我们没有看到能够证明官方说法的证据。国内媒体报道的都是汉族受到伤害。</strong>我认为，现在民族仇恨的东西正在聚集力量。我很担心这个东西会扩大到别的地区，民族仇恨是很难控制的。<br />
<br />
德国之声：您提到民族仇恨的问题，现在在新疆是不是汉族和维吾尔族之间的矛盾已经到了临界点了呢？<br />
<br />
伊力哈木：我觉得民族矛盾在”7.7″之后就开始聚集，很严重的聚集。但这个东西现在才有的吗？以前就有，只不过没有达到相互仇恨、普遍仇恨的程度。但是现在我认为，维吾尔族和汉族民众间的信任几乎没有了。很多人从民族的角度进行思考。我认为民族仇恨已经形成了，如果控制不下来，还以民族族群的角度采取征服者的态度，任由这样的事件再发生，人家会看到的。可以看到，过去几天汉族在示威的过程中也喊了一些口号，看得出来他们对地方当局也是不信任的。维吾尔人早就不信任了。别的地方一般有诉求都是对政府的，新疆这里很奇怪，他们（维、汉）都对政府不信任，但相互也不信任，冲突在加剧。<br />
德国之声：上周六中国官方宣布，撤销乌鲁木齐市委书记栗智的职务，但是被乌鲁木齐示威群众喊出”下台”的自治区区委书记王乐泉仍然在位。您怎样看待新疆最近的人事调整？<br />
<br />
伊力哈木：新疆的问题不是换某个领导人就可以解决的问题。很明显，撤换乌鲁木齐市委书记和公安厅厅长的做法是安抚汉族民众。官方对汉族示威游行者的态度和对维族人的态度明显不一样。新疆的维族人给我打电话说，维族人不许举行集会，军队拿着武器对着维族人，维族人根本不敢上街。</div>

							
							
							
							
