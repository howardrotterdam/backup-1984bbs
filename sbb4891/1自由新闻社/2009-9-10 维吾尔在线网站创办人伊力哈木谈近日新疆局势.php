一个美国人眼中的新疆事件<br />
<br />
（美国）Joseph Torigian/文<br />
<br />
<strong>乌鲁木齐“7・5”事件是一场悲剧。暴乱中维吾尔族人对无辜而又手无寸铁的汉族人民实施的暴力是没有任何理由可以为之辩护的</strong>。中国政府随即允许外国记者进入新疆并且很快就从其角度作出了宣传。但是中国人认为他们并没有得到国际上的支持和同情。这就提出了这样一个问题：中国在该事件的国际宣传上有没有失误之处？<br />
<br />
<br />
在回答这个问题之前，作以下两点说明是很重要的。第一，说国际上“反华”或不理解中国在新疆暴乱事件上的立场是言过其实了。国外媒体并非铁板一块，对事件有各类报道，且观点不一。有意思的是，在政府层面上来看，美国政府比非西方的铁杆穆斯林国家更支持中国。王光亚就此次暴乱表示“感谢美国政府目前的温和立场”；而穆斯林国家如土耳其与伊朗则对中国持批评态度。<br />
<br />
其次，不管中国的宣传如何以及人们有多了解中国，国际上还是会继续批判中国的政策的。这并不一定是因为他们是反华的，而是因为他们对如何解决中国的问题有不同看法。中国形象在国际公关领域的改善并不意味着所有人都会一路支持中国的政策。<br />
<br />
然而，在全球化时代，如何向外界传递信息也是非常重要的。中国也许会说，关于新疆事件，他们所说全部属实，因而公关形式就无关紧要了。但是，如果包装形式不合外国人的口味，最终还是实现不了其目的。愤然谴责倒可泄恨，但于事无补，只不过又是一个阿Q罢了。笔者想就中国在“7・5”事件上的国际公关提几个观察点。<br />
<br />
首先，中国的政治用语的英文翻译欠佳。英语里的“宣传”这个词有很重的贬义。它指的是某政府或团体捏造信息以控制舆论。从中文翻译过去的英文，或中国人所写的英文在西方人听来往往就像是此类宣传。感情色彩强烈的用词和冗长的句子在汉语里也许很正常，但在英语里显得牵强造作。把世界维吾尔代表大会称作 “热比娅之流”，说反对中国的人最终会“be hoisted by their own petard”等等，在英语里都显得很傻。不那么激烈的言辞会让人容易接受一点。如果中国在理，又何需慷慨陈词呢？<br />
<br />
第二，谩骂外国媒体是无济于事的。外国媒体是全世界了解中国的窗口。中国的反外国媒体之战，如反CNN，肯定无法改变他们的报道。外国记者坚信他们的报道的客观性，而且在他们看来，照片说明文字上及其它地方的失误都是很小的错误，并不足以成为“反华”证据。虽然错误应当被指出以澄清事实，中国并无讨好外国媒体的必要，但中国还是可以在说明事实与保持良好的媒体关系间找到一个良性平衡。<br />
<br />
第三，具体到“7・5”事件而言，中国恐怕是弄错了攻击对象。许多中国人之所以认为外国媒体是有偏见的是因为外国媒体的焦点都聚集在此次暴乱的政治、社会和经济根源以及中国政府是否行为过当。攻击热比娅・卡迪尔，展现新疆与中国在历史上的和睦关系的种种证据并没有回应这些问题，反而让人觉得中国把这些问题归咎于“外来势力”，自己却不承担任何责任。更糟的是，中国对外来势力加以指责却当时没有立刻提供证据。<br />
<br />
这从而带来了两个问题。第一，这反使热比娅扬名了。在“7・5”之前，西方还没有几个人听说过热比娅这个名字，但现在她恐怕要成为维吾尔的达赖喇嘛了。第二，说新疆自古是中国的领土听起来就像是搞宣传。新疆和西藏当然是中华人民共和国的领土，但是反复强调这种历史渊源，让人觉得这是向西方解释中国占有该地区的借口。也许我说的并不对，但如果中国的策略着眼于当今，效果会好得多。<br />
<br />
中国的崛起有着重大的历史意义，也应该得到全世界的认可。2008奥运极大提高了中国的民族自信心。满怀信心的中国能以新的风格改变世界眼中的中国。中国再不必只展示其好的一面来说明她的伟大，而把批评都说成是恶意的。真相不是任何个人或组织所能“提供”的，正如鲍勃・伍德沃德所言，“真理终会浮出水面”。好的公关运作会带来收益，但是诚恳、开放的态度和自我反思会使中国得到真正的尊重。</div>

							
							
							
							
