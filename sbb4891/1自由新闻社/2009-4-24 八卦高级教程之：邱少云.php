<br />
既然有如此低级的错误，那么这份回忆的可靠性就要大打折扣了。至于这些错误<br />
究竟是老人记忆错误所致，还是记者水平低劣歪曲老人回忆，就不得而知了。无<br />
论如何，跟关于邱少云的其他报道，如郑大蕃的通讯《伟大的战士邱少云》，比<br />
起来，曾纪有的这份回忆是很难让人采信的。<br />
<br />
关于战斗时间和兵力的问题已经解决。那么第二个问题，即弹药殉爆问题又是怎<br />
么回事呢？根据郑大蕃的通讯《伟大的战士邱少云》，邱少云是携带了冲锋枪和<br />
爆破筒的，而根据曾纪有的回忆，邱少云只带了把大钳子以剪开铁丝网之类的障<br />
碍物。如果曾纪有的回忆是真的，那么问题迎刃而解，因为大钳子显然是不可能<br />
爆炸的。如果郑大蕃的记载为真，那也并不代表邱少云的事迹就是虚构的。所谓<br />
在火中弹药一定爆炸只是外行想当然的说法而已。以炸药来说，TNT、硝酸铵都<br />
不会被火引爆，发射药（黑火药、单基/ 双基无烟火药）对火比较敏感，但是对<br />
于整装弹药（子弹、炮弹）来说，由于药筒、弹壳的隔离效果，如果温度不达到<br />
数百度并且持续一定时间，也不会被引燃。火工品（雷管、底火）比较敏感，但<br />
是邱少云身上显然不会大量携带，而且这些东西通常都装在专门防爆防火的包装<br />
盒内，也不容易被普通的火焰引发殉爆。事实上，机枪这类速射武器持续射击后，<br />
枪膛温度会超过400度甚至更多，即使是这样极端的情况下，枪弹合膛后弹药发<br />
生提前发火的故障的概率也比较低。而根据郑大蕃的记载的记载，邱少云牺牲前<br />
是把武器压在了身下的，他的身体起到了隔绝火焰高温的作用。从另一方面看，<br />
邱少云身上的火焰主要是荒草和棉衣引起的，这样的火焰恐怕难以达到让弹药殉<br />
爆的高温。根据事后负责寻找邱少云遗体的作战参谋梁嵩山的回忆(附录二)，<br />
“在夜色下，我看到邱少云烧焦的遗体蜷缩着，身上的军衣及胶鞋全都烧光了” <br />
，可见遗体大致保存完好，不然也不会提到军衣和胶鞋被烧光的情况，换句话说，<br />
就是当时火焰的温度并非很高，难以达到让弹药殉爆的地步。<br />
<br />
最后一个问题，即邱少云是否被燃烧弹直接击中牺牲。对于这一点，新闻记者郑<br />
大蕃已经提出过质疑，但是邱少云身边的战友证实了邱少云在烈火燃烧时还保持<br />
着神智清醒。而且志愿军也并非无英雄可以宣传而必须推出一个“假英雄”，因<br />
为紧接下来的上甘岭战役里，志愿军英雄比比皆是，随便举一个出来都是惊天地<br />
泣鬼神的壮举。至于“人在烈火中会身体会本能地扭动，这是不以人的意志为转<br />
移的”，我不清楚作者有什么生理学和统计学上的根据，但是通过这种想当然的<br />
所谓生理本能恐怕是无法解释战争的很多例子的，例如当有外物飞来时人的本能<br />
都是躲避，但是为什么黄继光可以用胸膛去堵正在猛烈发射的机枪呢？这恐怕难<br />
以用生理本能来做解释吧。<br />
<br />
事实上，西风独自凉文章里提到的“安陆市乡村教师廖忠明”的质疑还包括下面<br />
的内容：<br />
<br />
“文中数据表达模糊。一个“中午时分”就是几个小时的误差，“整个潜伏部队<br />
究竟是多少人？在一个山坡的草丛中能潜伏一支多大的部队？歼灭了全部敌人”，<br />
也不知道是多少。这些数字成年人都不知所云，何况是小学生。”<br />
<br />
显然，这样的质疑是很不合理的。小学课本又不是战史，完全没有必要去写邱少<br />
云牺牲的具体时间，更没必要去介绍潜伏部队的兵力和391高地守军是一个加强<br />
连。有兴趣的人，随便找一本战史都可以了解到具体情况。想必西风独自凉网友<br />
也认为这段质疑是荒谬的，所以没有提到这一段话。<br />
<br />
一个历史事件，不同书籍的记载，不同人的回忆，都可能有主观或者客观有意或<br />
者无意的偏差(例如391高地战斗的时间)，而涉及到这个历史事件的一些具体情<br />
况，也很有可能和你自己知识范围内的“常识” (例如弹药遇火就会爆炸)是不<br />
同的。遇见这样的情况时，首先应该去仔细查证资料来解决自己的疑惑，而不是<br />
立刻连联想到“虚构” “误导” 之类的词语。所以，在质疑英雄之前，请先质<br />
疑一下自己的“质疑” 。<br />
