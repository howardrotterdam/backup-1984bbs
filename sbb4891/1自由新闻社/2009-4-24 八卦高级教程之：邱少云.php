时，团里根据上级的指示必须潜伏打击敌人，87团3营9连，也就是邱少云和锁德<br />
成所在的连担任主攻任务，该连从高地北坡潜伏待命，同时7连佯攻，8连担任预<br />
备队。 <br />
<br />
　　10月11日晚9时，各连根据不同任务出发潜伏391高地。 <br />
　　梁嵩山所带领的团部侦察排同时潜伏在外围距离高地500多米的地方为主攻<br />
担任增援。 <br />
　　梁嵩山说，12日中午，391高地突然遭敌人的燃烧弹袭击，他们侦察排在草<br />
丛里清楚地看见，391高地浓烟滚滚，烈焰腾空，他和战友们捏着一把汗。 <br />
　　梁老说，这场战斗持续了20多分钟，打得十分漂亮，战斗结束后，部队马上<br />
撤离了高地，俘虏了十三四名敌人。 <br />
　　潜伏三夜找回英雄遗体 <br />
　　梁嵩山回忆说，战斗结束后，有关邱少云牺牲的情况很快汇报到了团部，经<br />
过清查，战斗中只牺牲了邱少云一个战士。 <br />
　　10月14日，梁嵩山接到团部的命令：带领侦察排寻找邱少云的遗体。梁嵩山<br />
说：“由于战斗结束后，我方迅速撤离了391高地，敌人恢复了被总攻时炸毁的<br />
铁丝网，高地也被后援敌人占领，搜寻十分困难！”10月14日晚上9时许，梁嵩<br />
山带领侦察排50多名战士，迅速潜伏，匍匐着向391高地邱少云牺牲的地方靠近。 <br />
<br />
　　虽然团部距离高地不远，但必经之地有反坦克沟、交通壕、铁道、一片丘陵<br />
地带，到达高地十分困难，而他们晚上潜伏寻找，必须赶在黎明前返回团部。 <br />
　　14日、15日连续两个晚上都没有找到邱少云的遗体。 <br />
　　16日晚，梁嵩山继续带着侦察战士潜伏到391高地。 <br />
　　17日凌晨1时许，他们终于在391高地一片烧光的坡地上找到了邱少云的遗体。 <br />
　　梁嵩山老人比划着说：“在夜色下，我看到邱少云烧焦的遗体蜷缩着，身上<br />
的军衣及胶鞋全都烧光了，我们心情十分沉痛，就用帆布雨衣将邱少云的遗体包<br />
裹起来赶紧运回团部！”印象中邱少云平淡可爱 <br />
<br />
　　梁嵩山说，他们部队1951年2月参加抗美援朝，他和邱少云同在87团3营，他<br />
在7连任排长，邱少云在9连当战士。当时，不少新战士知道要去参加抗美援朝，<br />
思想上有情绪，当时邱少云可能入伍时间不长，记得全营开大会时，教导员王葵<br />
远还点名给邱少云等做思想工作，鼓励他们积极上进。 <br />
<br />
　　梁嵩山笑着说：“那时候，我印象中的邱少云和其他战士一样，有平淡、可<br />
爱和真实的一面！后来，我调到团部，再没和邱少云见过面！”说起老战友邱少<br />
云和自己带领战士找回邱少云的遗体的经过，梁嵩山老人感慨万千。今天更没有<br />
想到邱少云的班长、也是87团的老战友锁德成依然健在。“我很高兴看到老战友<br />
锁德成在《兰州晨报》上发出‘英雄帖’，我们应该怀念并永远记住邱少云的名<br />
字和精神！”<br />
<br />
<br />
附录3：<br />
邱少云的战友讲述被历史遗漏的细节<br />
<br />
　　抗美援朝战争中，涌现了不少顶天立地的志愿军英雄，邱少云就是这样一位<br />
令人敬仰的英雄。近日，曾先后担任邱少云所在的3班副班长、所在的1排排长的<br />
曾纪有，讲述了邱少云当年一些鲜为人知的故事。<br />
　　曾是“被帮扶对象”<br />
　　“1950年3月的一天，我从医院看病回来，”曾纪有向记者回忆说，“连队<br />
的一名干部将三个小伙子领到我面前说，部队招了一批新兵，这三个分到你班里。<br />
其中一个就是邱少云。”<br />
　　新兵入伍要参加培训，沉默寡言的邱少云表现不算很积极；在参加劳动时还<br />
有些自私，拿着一把好铁锹就不愿跟其他人换，有一次因跟一名老兵争铁锹，竟<br />
打了起来。见此情况，作为副班长的曾纪有要拉他一把，就将他列为自己的“帮<br />
扶对象”，经常给他开“小灶”，谈心。渐渐地，邱少云纪律性有了很大好转。<br />
　　入朝后变成标兵<br />
　　当年10月，在朝鲜战争爆发后，第10军改为志愿军第15军，师以下编号不变，<br />
从简阳开进朝鲜参战。此时曾纪有改任1排排长。<br />
　　进入朝鲜后，部队加强了训练力度和思想教育力度，在革命的大熔炉里邱少<br />
云进步很快，他对武器特别有悟性，部队装备的苏式武器如转盘冲锋枪、爆破筒、<br />
莫洛托夫手雷，他一摸就会，成为了一名标兵。<br />
　　英雄壮烈牺牲<br />
　　1952年10月，志愿军决定打响上甘岭战役。要取得战役胜利，必须先拿下<br />
391高地。指挥部决定派一支部队秘密潜入敌军后沿的半山腰，埋伏24小时，一<br />
旦进攻时间到，迅速抢占391高地。曾纪有所在的一排担起了这个重任。<br />
　　10月23日下午6时，我军向敌军阵地猛烈发射炮弹，趁敌军慌乱之机，1排52<br />
名官兵(本为48人，上级临时增加4名医护及话务人员)浑身插满芦草匍匐前进，<br />
三小时后，部队静悄悄地进入预定地点，我军炮弹也停止了。<br />
　　“这次行动特别重要，上级对我们的要求也十分严格，进入阵地后，我们只<br />
能脸埋在地上，双手趴在地上，一动也不能动地待命。因为我们只距离敌人阵地<br />
30米左右，稍发出点声响，弄出点动静，就可能被发现。”<br />
　　50多年过去了，但曾老对那场战斗记忆犹新。“那天，刚下完一场雪，万物<br />
萧瑟，大地一片寂静。也许是太沉静了，敌人反觉得不安，怀疑我军搞什么名堂，<br />
可是又不敢出来巡逻，就不时地对周围地带进行骚扰。先从碉堡里打出十几发烟<br />
雾弹和毒气弹，我们戴上口罩，敌人没弄着我们，反呛着了自己，我们能清楚地<br />
听到他们咳嗽、骂娘声。下午2时左右，敌人又向周围打出数百发炮弹，其中不<br />
少落在了我们的潜伏区，一些人受伤了，也有人阵亡了，但我们还是纹丝不动。”<br />
　　曾老接着说，“约下午4时，敌人又打出来数十发燃烧弹，其中有4发落在了<br />
我们埋伏区，顿时火熊熊燃烧起来。其中一发正落在了邱少云身边，火很快烧着<br />
了他身上的草。我就埋伏在他身后5米的右方，看得清清楚楚。我心提到了嗓子<br />
口，因为邱少云是尖刀班战士，负责战斗打响后剪断敌人的铁丝网，所以埋伏较<br />
靠前，在第3排，离敌军铁丝网只5米左右，他只要稍动一下，就有可能被发现，<br />
整个排也就会被发现，整个行动也就失败。但是英勇的邱少云同志自始至终没有<br />
动一下，任由全身在燃烧。火在邱少云身上燃烧了半个多钟头，我眼睁睁地看着<br />
邱少云由一个活生生的人慢慢变成一具焦体，心里难受得像刀在剜。”<br />
　　一些真实的细节<br />
　　据曾纪有介绍，对于邱少云牺牲的细节，后来所见的文章有一些不实的地方，<br />
他就此也回答了相关问题。<br />
　　“有些文章上说邱少云身边有一条水沟，他若滚进沟里就能活命。是这样<br />
吗？”<br />
　　“不是这么回事。我们在半山腰，哪来水沟，有水沟，在那寒冬也没有水。”<br />
　　“有文章说，当时有些士兵为救邱少云，向连长程子英请示打响战斗……”<br />
　　“这是不可能的，当时阵地上最高指挥官就是我。我们的任务就是埋伏待命，<br />
谁也不准乱动，话也不能说一句。”<br />
　　“很多文章里说，当时你们有一个营500多人潜伏到敌方前沿阵上，怎么才<br />
52人？”<br />
　　“那是错误的，就52个，也不是潜伏到敌方前沿，而是后山腰。”曾老拿出<br />
笔，给记者画了一张当时的地形图。“这项任务极为秘密，不可能派那么多人去。<br />
我是这支队伍的最高指挥官，师部单线直接和我联系，除了我和师部领导，谁也<br />
不知这项任务。当时师部还派了一名干部来协助我，可惜还没进潜伏地就被敌人<br />
的流弹打伤撤下了。”<br />
　　摘自《老年生活报》<br />
