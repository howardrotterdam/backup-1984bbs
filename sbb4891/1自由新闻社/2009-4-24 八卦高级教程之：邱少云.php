													<h2>课文：我的战友邱少云</h2>
						
						
																											<div id="postmessage_80544" class="t_msgfont">敌人控制的“391”高地，像一颗毒牙，a入我们志愿军阵地。我们准备在黄昏时分发动突然袭击，拔掉这颗毒牙，把战线往南推移。 <br />
<br />
那一天，天还没有亮，我们连悄悄地模进“391”高地下面的山拗，潜伏在一条比较隐蔽的山沟里。太阳渐渐爬上山头。我发现前面六十多米的地方就是敌人的前沿阵地，不但可以看见铁丝网和胸墙，还可以看见地堡和火力点，甚至连敌人讲话都听得见。敌人居高临下，当然更容易发现我们。我们趴在地上必须纹丝不动，咳嗽一声或者蜷一下腿，都可能被敌人发觉。我看了一下前面，班长和几个战士伏在枯黄的茅草丛里。他们身上披着厚厚的茅草作伪装，猛一看去，很难发现他们。我又看了看伏在我身边不远的邱少云。他也全身伪装，隐蔽得更好，相隔这么近，我几乎找不到他。 <br />
<br />
我们的炮兵不断地轰击敌人的阵地，山顶上腾起一团一团的青烟。敌人阵地前沿的地堡一个接一个被掀翻了。看着这种情景，我只盼望天快点儿黑，好痛痛快快地打一仗。 <br />
<br />
到了中午，敌人突然打起炮来，炮弹一排又一排，在我们附近爆炸。显然，敌人已经感觉到他们的前沿阵地不太安全了，可是没有胆量冒着我军的炮火出来搜索，只好把看家的本领“火力警戒”拿出来了。 <br />
<br />
排炮过后，敌人竟使用了燃烧弹，我们附近的荒草着火了。火苗子呼呼地蔓延，烧得枯黄的茅草毕毕剥剥地响。我忽然闻到一股浓重的棉布焦味，扭转头一看，哎呀！火烧到邱少云身上了！他的棉衣已经烧着，火苗趁着风势乱窜，一团烈火把他整个身子包住了。 <br />
<br />
这时候，邱少云只要从火里跳出来，就地打几个滚，就可以把身上的火扑灭。我趴在地附近，只要跳过去，扯掉他的棉衣，也能救出自己的战友。但是这样一来，我们就会被山头上的敌人发觉，我们整个班，我们身后的整个潜伏部队，都会受到重大的损失，这一次作战计划就全部落空了。 <br />
<br />
我的心绷得紧紧的。这怎么忍受得了呢？我担心这个年轻的战士会突然跳起来，或者突然叫起来。我不敢朝他那儿看，不忍眼巴巴地看着我的战友被活活地烧死。但是我忍不住不看，我盼望出现什么奇迹――火突然间熄灭。我的心像刀绞一般，泪水模糊了我的眼睛。 <br />
<br />
为了整个班，为了整个潜伏部队，为了这次战斗的胜利，邱少云像千斤巨石一般，趴在火堆里一动也不动。烈火在他身上烧了半个多钟头才渐渐地熄灭。这位伟大的战士，直到最后一息，也没挪动一寸地方，没发出一声呻吟。 <br />
<br />
黄昏时候，漫山遍野响起了激动人心的口号：“为邱少云同志报仇！”我们怀着满腔怒火，勇猛地冲上“391”高地。敌人全部被我们歼灭了。看看时间，从发起冲锋到战斗结束，才20分钟。 <br />
<br />
我永远忘不了那一天――1952年10月12日。<br />
