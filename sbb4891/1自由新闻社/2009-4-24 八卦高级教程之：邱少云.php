曾纪有是广西兴安县人，今年已是78岁的老人。<br />
<br />
1946年9月，刚结婚不久的曾纪有被国民党军队抓壮丁，从此离开故里，转战南北。1949年4月在与解放军作战时，已是国民党军队排长的曾纪有率12名士兵投诚，从此成了一名解放军战士。随着解放大军节节胜利，曾纪有所在的10军一路南下、西进，到达四川。四川解放后，驻扎在简阳县(今简阳市)的部队(29师87团3营9连)改为简阳县大队，负责在简阳剿匪，曾纪有在这支部队里担任9连1排3班副班长。<br />
<br />
“1950年3月的一天，我从医院看病回来，”曾纪有向记者回忆说，“连队的一名干部将三个小伙子领到我面前说，部队招了一批新兵，这三个分到你班里。其中一个就是邱少云。邱少云那时18岁，他说是简阳县养马河山茶村人。他一米六五左右的个子，一脸肉疙瘩，人长得很结实，但不爱说话，在班会上有时要点他的名才发言说几句。”<br />
<br />
