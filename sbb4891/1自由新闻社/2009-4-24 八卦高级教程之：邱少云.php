																											<div id="postmessage_80525" class="t_msgfont">安陆教师廖忠明质疑课文《我的战友邱少云》<br />
 <br />
(2004-03-20 06:53:11)<br />
 <br />
荆楚在线消息（楚天金报）记者夏一兵通讯员刘家连报道：“小学现行教材《我的战友邱少云》课文有些情节不准确。”3月18日，安陆市乡村教师廖忠明，在汉将他的质疑向有关部门进行了反映。<br />
<br />
《我的战友邱少云》是现行小学语文教材第九册的课文。廖忠明说，文中有些内容不太符合军事常识。邱少云在投入战斗前被烧死，他随身携带的武器（如手榴弹、爆破筒）在燃烧过程中为什么未爆炸？邱少云是如何做到至死不暴露目标？他的武器是怎样处理的？文中只字未提。<br />
<br />
课文内容不符合战争逻辑。邱少云埋伏的地点距敌人只有60多米，能听到敌人的讲话声，这让人生疑。如果只有这么近的距离，烈火在邱少云身上燃烧半个多小时，敌人大白天为什么未发现目标？<br />
<br />
文中数据表达模糊。一个“中午时分”就是几个小时的误差，“整个潜伏部队”究竟是多少人？在一个山坡的草丛中能潜伏一支多大的部队？“歼灭了全部敌人”，也不知道是多少。<br />
<br />
以上疑问，在抗美援朝随军记者郑大藩当年采写的通讯《伟大的战士邱少云》中都不存在。廖忠明拿出郑大藩的人物通讯复印件。记者看到，繁体字竖排的通讯中，介绍邱少云的事迹十分清楚，没有小学课本中的漏洞。<br />
<br />
廖忠明建议，有关部门应对这篇课文进行修改。<br />
<br />
廖忠明当过兵，目前是安陆市辛榨乡四报高小语文老师。1995年曾对德国著名作家柏吉尔所写的《琥珀》一文提出疑问，所写论文《‘琥珀’的假想不能成立》获优秀论文奖。<br />
