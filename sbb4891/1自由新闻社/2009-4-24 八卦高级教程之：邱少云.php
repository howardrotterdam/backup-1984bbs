													<h2>斗胆质疑邱少云</h2>
						
						
																											<div id="postmessage_80552" class="t_msgfont">作者 21cv 发表在：百灵社区<br />
<br />
“小学现行教材《我的战友邱少云》课文有些情节失真。”3月18日，安陆市乡村教师廖忠明，将疑问向有关部门进行了反映。<br />
<br />
《我的战友邱少云》，是现行小学语文教材第九册的课文。廖忠明说，至少有三处内容不符合军事常识。邱少云在投入战斗前被烧死，他随身携带的武器(如手榴弹、爆破筒)在燃烧过程中为什么不爆炸？邱少云是如何做到至死不暴露目标？他的武器是怎样处理的?文中只字未提。<br />
<br />
 课文内容不符合战争逻辑。邱少云埋伏的地点距敌人只有60多米，能听到敌人的讲话声，这让人疑问。烈火在邱少云身上燃烧半个多小时，居高临下的敌人大白天为什么不能发现目标？ 文中数据表达模糊。一个“中午时分”就是几个小时的误差，“整个潜伏部队究竟是多少人？在一个山坡的草丛中能潜伏一支多大的部队？歼灭了全部敌人”，也不知道是多少。这些数字成年人都不知所云，何况是小学生。 以上疑问，在抗美援朝随军记者郑大藩当年采写的通讯《伟大的战士邱少云》中都不存在。<br />
<br />
廖忠明拿出郑大藩的人物通讯复印件。记者看到，繁体字竖排的通讯中，介绍邱少云的事迹十分清楚，没有小学课本中的漏洞。廖忠明建议，教育部门应对这篇课文进行修改。<br />
<br />
附：《斗胆质疑邱少云》 <br />
<br />
邱少云是上个世纪家喻户晓的英雄，我们这代人从小就受到了这位英雄的鼓舞。他的事迹感人致深，在上个世纪是几乎没有也不可能有人敢于公开质疑这位英雄的。时代毕竟在前进，所谓江山代有英雄出，各领风骚若干年。<br />
<br />
21世纪是理性的世纪，经历了上个世纪特有的非理性剧变后的中国，不会再次走进那个痛苦的记忆了，这也是中国不断前进的必要条件。理性的时代必然有理性的思考，即使铁定的英雄也不能例外，这是一个基本原则。不过我首先要说的是，我们必须有一个共识：思考无罪，质疑无罪。<br />
<br />
考虑到我的想法不会得到一致的同意，所以还是在文章的题目里加上“斗胆”二字比较好一些。 <br />
<br />
英雄就是英雄，有什么可以质疑的呢？我们先来看看有关邱少云的一段文字：邱少云是中国人民志愿军一级英雄。战士。四川省铜梁县人。1949年12月参加中国人民解放军。作战勇敢，意志坚强。1951年3月参加中国人民志愿军赴朝作战。部队开赴前线途中，他冒着美军飞机的扫射轰炸，从燃烧的居民房屋里救出1名朝鲜儿童。1952年10月，所在部队担负攻击金化以西美军为首的联合国军前哨阵地391高地。高地前沿是一片开阔地，为缩短进攻距离，便于突然发起攻击，11日夜，部队组织500余人在敌阵地前沿潜伏，他所在排潜伏在高地东麓距敌前沿阵地仅60多米的蒿草丛中。12日12时左右，美军盲目发射燃烧弹，其中一发落在他潜伏点附近，草丛立即燃烧起来，火势迅速蔓延到他身上，燃着了棉衣。为了不暴露目标，确保全体潜伏人员的安全和攻击任务的完成，他放弃自救，咬紧牙关，任凭烈火烧焦头发和皮肉，坚持30多分钟，直至壮烈牺牲。反击部队在邱少云伟大献身精神鼓舞下，当晚胜利攻占了391高地，全歼美军1个加强连。战后，所在军party委根据他生前意愿，追认他为中国.party员，并追授模范青年团员称号。1952年11月6日，中国人民志愿军总部给他追记特等功。1953年6月1日追授他一级英雄称号。同年6月25日，朝鲜.主义人民共和国最高人民会议常任委员会追授他朝鲜.主义人民共和国英雄称号和金星奖章、一级国旗勋章。1953年，四川省铜梁县建立邱少云烈士纪念馆，并在凤凰山顶树立邱少云烈士纪念碑，朱德为之题词。（《军事百科研究》提供） <br />
<br />
这就是有关邱少云英雄事迹的最权威记录，也是我们在小学时代接受的教育内容。的确，邱少云是一位优秀的志愿军战士，他生前肯定做过不少好事这无可置疑。我要质疑的是有关邱少云牺牲的细节问题。 <br />
<br />
记录说“火势迅速蔓延到他身上，燃着了棉衣。为了不暴露目标，确保全体潜伏人员的安全和攻击任务的完成，他放弃自救，咬紧牙关，任凭烈火烧焦头发和皮肉，坚持30多分钟，直至壮烈牺牲。”从这一段文字我们理解，邱少云就是为了不暴露目标，一动不动地趴在原来地位置上，被大火活活烧死了。正是邱少云以极大的毅力，忍受这烈火的灼烧，才没有被敌人发现志愿军的攻击计划。 就是这段文字，表面看来没有什么问题，但里面蕴含着起码的逻辑错误。 <br />
<br />
邱少云干什么去了？打仗去了。他作为志愿军战士打仗时带武器吗？肯定带，据说当时志愿军战士使用的武器不但有中国制造的，还有苏联制造的。那么邱少云带武器时带不带弹药？肯定带，起码要带一定数量的子弹，否则手里的枪和烧火棍就没有区别了。<br />
<br />
按照文章的描述，当时邱少云只要动一动就有可能被美军发现，叫一声或者是本能地喊一声也就极有可能被发现。所以，他是不可能将武器和弹药移动到火区以外的地方，否则也会被发现。也就是说，邱少云直到被烧死，他的武器弹药一直在身边，或者说就是在他的身上携带着。如此一来，武器弹药必然在大火中灼烧了。稍有思维能力的人都明白，弹药在火里灼烧时会发生什么现象。毫无疑问，弹药必然要爆炸。而爆炸声是非常巨大的，近在咫尺的美国人肯定不是聋子，为什么没有听到呢？这就是我要对英雄牺牲细节的质疑。 所以，我觉得关于邱少云在战场上的细节描写是虚构的。<br />
<br />
我的意思不是说邱少云不是英雄，我也不怀疑邱少云牺牲在朝鲜战场的事实，只是想明白英雄究竟是怎么牺牲的，为什么有人虚构出一个有逻辑错误的故事来欺骗读者？英雄已经逝去，无法回答我的问题了，写这段文字的人为什么不顾起码的逻辑而虚构呢？或许作者就是为了突出英雄的顾全大局的英雄主义，或许就是为了迎合宣传的需要吧。总之，邱少云即使真的被烧死了，也绝对不可能没有被美军发现。 50多年以后这样质疑英雄，的确对英雄本人有点大不敬了，但不质疑又觉得自己被欺骗了被愚弄了，这种感觉也非常不好受。成为英雄的首要条件就是英雄的事迹必须真实，不能把不存在的事情强加在英雄的头上欺骗别人。如果不能说明邱少云的真实牺牲细节，那么我们就有权怀疑英雄的牺牲细节真伪和宣传的动机。 <br />
<br />
我们的时代需要英雄，但不需要假英雄；我们需要宣传机构的真实宣传，但不需要欺骗。类似的事情还有不少，比如雷锋日记，他是汽车司机，但他的日记从头到尾看不到一点关于汽车的描述，尽是一些无比正确的政治说教和好人好事。而且雷锋充其量不过是个小学没有毕业的文化程度，而他的日记水平简直可以和大学教授的文笔论高下了。“人的生命是有限的，可是，为人民服务是无限的，我要把有限的生命，投入到无限的为人民服务之中去。”“对待同志要象春天般的温暖，对待工作要象夏天一样火热，对待个人主义要象秋风扫落叶一样，对待敌人要象严冬一样残酷无情”“一个人的作用，对于革命事业来说，就如一架机器上的一颗螺丝钉。机器由于有许许多多的螺丝钉的联接和固定，才成了一个坚实的整体，才能够运转自如，发挥它巨大的工作能。螺丝钉虽小，其作用是不可估计的。我愿永远做一个螺丝钉。螺丝钉要经常保养和清洗，才不会生锈。人的思想也是这样，要经常检查，才不会出毛病。我要不断地加强学习提高自己的思想觉悟，坚决听party和毛主席的话，经常开展批评与自我批评，随时清除思想上的毛病，在伟大的革命事业中做一个永不生锈的螺丝钉。”……<br />
<br />
看了雷锋的日记我简直认为他是一个地地道道的大学中文系教授了。我不禁怀疑，是不是也有人在雷锋日记里造假？ 没有英雄的民族是不幸的，有了英雄不知道珍惜英雄的民族则是可悲的。这是邱吉尔的名言。我认为，如果按照邱吉尔的逻辑理解，那么，没有英雄的民族是不幸的，但有了英雄，却在英雄头上大量造假的民族简直是耻辱的。我真的不知道，我们那么多的英雄究竟有多少被包装过，被按照特定需要而造了假，然后展现在人们面前。难道孔夫子的“民可使由之，不可使知之”就真的成了铁定的愚民教义了？ <br />
<br />
不管邱少云是怎么死的，就凭他在朝鲜战场作战的事实，他这个英雄就是值得我们永远铭记的。不管别人在他名下怎么造假，我丝毫不怀疑邱少云是英雄。我只是想通过质疑英雄的牺牲细节，揭露那些虚伪的宣传家们是多么弱智，为了达到宣传英雄的目的，不惜造假，坚定恪守“不可使知之”的古训，这是我们时代的悲哀。 <br />
<br />
邱少云牺牲了，很多英雄也已经不在人世了，他们的英雄事迹永远值得后人缅怀。但造假者没有退出历史舞台，为了一些特定的需要，造假者还在造假，我们今后一定还能听到许多假英雄的假先进事迹。孔夫子的“民可使由之，不可使知之”势必千秋万代而不衰！势必永远成为权贵者的驭民金鞭！<br />
