邱少云，经四川省民政部门多次调查，是铜梁县关溅乡(现改为少云镇)人确实，他的哥哥邱东云(解放前由别人抱养，现还在世)，三弟邱少全于1991年病逝，小弟邱少华现仍住在少云镇农村，每年都来一次纪念馆。<br />
