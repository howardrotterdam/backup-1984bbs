<br />
一段时间培训后，邱少云便参加了剿匪斗争，曾纪有把他带在自己身边。5月的一天，曾纪有领导工作组外出执行任务，途经山茶村时，发现村旁有一座奇怪的屋子，屋顶一半是瓦一半是草，屋前一位中年妇女拉着一名10多岁的男孩站着。曾纪有心生怀疑，问邱少云：“这房子怎么这么怪，会不会是土匪的观察哨？”邱少云有些不好意思地答，“这是我的家。”曾纪有有些惊讶，说：“是嘛，那妇女是谁？男孩又是谁？”“是我妈和我弟。”“你爸呢？”“我爸已死了。”“想不想回家看看？”曾纪有问，邱少云答‘想’，但曾纪有没答应他，因为剿匪时期，不宜暴露家人目标。曾纪有看到邱少云眼里溢满了泪水，他也只有在心里叹一声气。<br />
<br />
当年10月，在朝鲜战争爆发后，第10军改为志愿军第15军，师以下编号不变，从简阳开进朝鲜参战。此时曾纪有改任1排排长。<br />
<br />
采访曾老之前，记者收集了不少有关邱少云英雄事迹的文章，记者注意到，曾老的回忆与这些文章有很大差异。“很多文章说邱少云是重庆市铜梁县关溅乡(今少云镇)人，他怎么是四川简阳人？而且邱少云纪念馆也是建在铜梁县。”记者问。<br />
