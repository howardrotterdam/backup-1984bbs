在所有关于英雄邱少云的文章中，都没有提到担任邱少云所在的3班副班长、所在的1排排长的曾纪有，而他恰是与邱少云最“亲密”的人。<br />
<br />
初春时节，在南宁市长路的烈士陵园，担任该园顾问已离休多年的曾纪有接受了记者采访，谈起了那段与邱少云共同战斗的岁月。作为邱少云的排长，他的回忆与公开报刊媒体所说的邱少云却有些不同。<br />
