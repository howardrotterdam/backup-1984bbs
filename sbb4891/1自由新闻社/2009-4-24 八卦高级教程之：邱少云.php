1997年，春节，在全区老军人座谈会上，曾纪有认识了周秀全，一问才知两人同是3营的战友！找到一个战友，曾纪有便想打听他那个排的战友情况。周秀全给了他一本15军军史。他翻开一看，1排只有邱少云一个人的名字。<br />
<br />
“为什么找不到全排的名单呢？当时的档案在哪？”记者问。<br />
<br />
“可能档案在部队调动过程中丢失了。战争年代，档案丢失是难免的事。”<br />
<br />
也许是因为档案丢失了，部队上不仅没有人知道曾纪有还活着，关于邱少云的资料也出现了许多与曾纪有所知道的很不一样的内容。<br />
<br />
1975年，在柳州疗养的曾纪有接受学校的邀请，给学生们讲讲邱少云的故事。那时的学生课本上登有邱少云的故事，虽然已读过，但由邱少云的战友来讲述他的故事，同学们还是听得津津有味。可听着听着，同学们就发现了“曾爷爷”讲的与书上说的很多不一样，一个个争相提问。<br />
<br />
这让曾纪有大吃一惊。于是他找来教材查看，果然有很多不同。后来他又找来有关报刊翻看，同样存在很多不同。他归纳了一下，大致有：<br />
<br />
报刊上的邱少云是重庆市铜梁县关溅乡(今少云镇)玉屏村邱家庄人，而他所知道的邱少云是四川省简阳县(今简阳市)养马河山茶村人。<br />
<br />
报刊上的邱少云是志愿军第15军29师168团3营9连1排3班，他所知道的邱少云是志愿军第15军29师87团3营9连1排3班。<br />
<br />
报刊说的邱少云赴朝作战是1951年3月28日，他所记得的是1950年10月23日，为首批入朝作战志愿军。<br />
<br />
报刊上的邱少云牺牲时为26岁，牺牲时间是1952年10月12日下午，他所知道的邱少云牺牲时为20岁，牺牲时间是1952年10月24日下午4时。<br />
<br />
“这一天同时也是我受伤的日子。我记得清清楚楚的。”曾纪有说。<br />
<br />
报刊上的邱少云家中有父母，有三个兄弟，他所知道的邱少云父亲去世，家中只有母亲和弟弟。<br />
<br />
报刊上的邱少云参加解放军前被国民党军队抓过壮丁，在川军十八团刘义的部队当伙夫，但曾纪有从未听邱少云提起过他有这段经历。<br />
<br />
那时连队经常举行诉苦会，邱少云在会上也只提他家庭的苦难，未提到他有当兵经历。<br />
<br />
报刊上的邱少云在剿匪斗争中多次立功，在资阳只身抓获匪首刘义，但曾纪有的记忆中，他们奔赴朝鲜前从未离开过简阳，且简阳县大队在剿匪期间并没有抓获过一个土匪。“当时有严格的纪律，我们外出执行任务是不许单兵作战的。”曾纪有说。 <br />
<br />
多方查证无法证实<br />
<br />
四川民政部门证实邱少云是铜梁县人<br />
