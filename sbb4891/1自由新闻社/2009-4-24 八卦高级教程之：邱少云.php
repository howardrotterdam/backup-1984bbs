													<h2>邱少云烈士生平</h2>
						
						
																											<div id="postmessage_80540" class="t_msgfont">邱少云，四川省铜梁县人，1931年生。1949年12月25日参加中国人民解放军，1951年3月25日参加中国人民志愿军。1952年10月12日，在朝鲜平康以南铁原东北的“391”高地的战斗中光荣牺牲。同年11月6日，中国人民志愿军领导机关决定追授特等功臣，并于1953年6月1日授予“一级英雄”称号。中国共产党志愿军某部委员会追认他为中国共产党党员。1953年6月，朝鲜民主主义人民共和国最高人民会议常任委员会授予“朝鲜民主主义人民共和国英雄”称号，同时授予金星奖章和一级国旗勋章。<br />
<br />
　　英雄差点被埋没<br />
<br />
　　邱少云牺牲后，连队给邱少云报请了三等功。据邱少云生前战友林炳远后来回忆说，那次战斗半个月后进行战斗总结，他和邱少云的指导员王明世一同到师里去汇报。当时，一位组织部门的干事要求王指导员举个具体的人或事来谈谈。<br />
<br />
　　王指导员于是提到了邱少云。并说，在执行任务前进行战前动员时，战士们很激昂，纷纷表了态，可邱少云却闷不吭声，因此连里对他很不放心。于是我找邱少云谈心，给他讲了许多革命道理，他也向我表了态：就是被敌人的子弹打中了，也不暴露目标，暴露目标就是革命的罪人。<br />
<br />
　　第二天，他向党支部交了一份入党申请书，在申请书中他表示：为了战斗胜利，愿贡献自己的一切。<br />
<br />
　　可不曾料到，第二天他便牺牲了，真的用年轻的生命换来了战斗的胜利。<br />
<br />
　　那位干事听了王明世的介绍，非常感动地说：这样的英雄行为，太感人了，三等功不行，应报特等功！<br />
<br />
　　于是，29师政治部立即上报邱少云特等功的报告，报请志愿军司令部批准。<br />
<br />
　　起初，个别领导认为：邱少云不过是战时特殊死亡，算不上英雄，三等功就可以了。但更多的同志认为他严守纪律，为了整体牺牲自我的精神难能可贵。后来，意见终于统一了，少数服从多数。1952年11月16日，中国人民志愿军领导机关追授邱少云为特等功臣。<br />
<br />
　　受关注再授军功<br />
<br />
　　1952年底，一位名叫郑大藩的随军记者就邱少云牺牲提出了两个问题：<br />
<br />
　　一是燃烧弹落在什么地方？是打中头部死亡还是一点一点烧死的？二是他身边有无水沟？谁看见了邱少云牺牲的全过程？<br />
<br />
　　后来，邱少云的战友李元兴回忆说：“燃烧弹打在邱少云前方两米左右，燃烧液溅到了他的身上，是一寸一寸烧过来的，从头烧到脚，当时我伏在他身后五米左右，亲眼见到大火一点一点把邱少云活活烧死。”<br />
<br />
　　李元兴和其他战友还证明，邱少云右面3米处有一条小水沟，如果邱少云愿意，只需侧向滚即可活命。<br />
<br />
　　面对这些证明材料和见证人的描述，记者郑大藩感动了，他含着眼泪奋笔疾书。<br />
<br />
　　《人民日报》用整版篇幅发表了他那篇著名长篇通讯《伟大的战士邱少云》。此文一出，随即在国内外引起强烈的反响。<br />
<br />
　　1953年6月1日，中国人民志愿军领导机关再次授予邱少云“一级战斗英雄”称号，并追认他为中国共产党党员，还授予他“模范共青团员”的称号。</div>

							
							
							
							
