【相关阅读：中国的核心利益】<br />
<a href="http://www.chinanews.com.cn/gn/news/2009/07-29/1794984.shtml" target="_blank">http://www.chinanews.com.cn/gn/news/2009/07-29/1794984.shtml</a><br />
