戴秉国指出，奥巴马总统上任后半年，中美关系开局不错，势头很好。中国愿意与美方一起坚定不移地致力于建设中美二十一世纪的全面积极关系，造福两国人民和全世界。<br />
“确保中美关系长期健康稳定地向前发展，很重要一条是相互理解、尊重支持对方，维护自己的核心利益。”戴秉国接着说，<strong>中国的核心利益第一是维护基本制度和国家安全，其次是国家主权和领土完整，第三是经济社会的持续稳定发展。</strong><br />
“我觉得这次对话是成功的，”戴秉国说，“但更重要的是要把说的变成做的。”<br />
戴秉国与王岐山还在记者会上一同感谢媒体。<br />
王岐山说，自己与媒体多年“打交道”，特别要感谢媒体的朋友们。“我们的对话经过你们的发散和宣扬，包括猜测和评析，传遍了全球。”<br />
王岐山说，如今任何会议若离开了媒体的宣传，影响力便会大大降低。“影响力有正面也有负面，有准确的，也有不那么准确的；我们靠媒体传播，就要承受传播中的瑕疵。”<br />
戴秉国也表示，媒体作用非常重要，他希望媒体的宣传能够推动中美关系发展，增进两国人民友谊。<br />
