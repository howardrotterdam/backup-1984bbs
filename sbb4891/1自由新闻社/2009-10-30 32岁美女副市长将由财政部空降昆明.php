													<h2>32岁美女副市长将由财政部空降昆明</h2>
						
						
																											<div id="postmessage_306434" class="t_msgfont">昆明迎来32岁美女副市长 曾任财政部副处长<br />
<br />
2009年10月30日09:06<br />
<br />
<img src="http://img.hexun.com/2009-10-30/121524090.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
李茜(资料图)<br />
<br />
　　昆明即将迎来两位新副市长，其中一位32岁。在昨日召开的昆明市十二届人大常委会第28次会议上，昆明市委建议大会审议两位副市长候选人――来自国家发改委的周小棋以及来自财政部的李茜。一旦大会审议通过，李茜将成为昆明最年轻的副市长，比此前的“70后”副市长阮凤斌还要年轻4岁。<br />
<br />
　　“省委常委会已研究同意，决定提名周小棋、李茜二人为昆明市政府副市长候选人，挂职2年。”受昆明市委的委托，昆明市委常委、市委组织部部长郭红波在会上作情况说明，他介绍，目前现代新昆明建设已经进入关键的第二个五年期，昆明正处在开创跨越式发展新局面的时期。城市轻轨建设、基础设施建设、多渠道筹集项目建设资金等各方面工作任务十分繁重，急需一批政治坚定、品行优良。作风过硬。熟悉业务，具有强烈创新意识和实干精神的优秀干部，投入到现代新昆明建设中。为此，在省委组织部的协调、中组部的大力支持下，国家发改委和财政部各选派了1名优秀的干部到昆明挂职担任副市长。<br />
<br />
　　郭红波介绍，“周小棋研究生毕业后，长期在铁道部计划司挂职，2004年6月调国家发改委工作，先后任交通运输司铁道处处长、基础产业司交通处处长，积累了比较丰富的工作经验，对投资项目管理及交通基础设施建设方面的业务比较熟悉。”<br />
<br />
　　来自国家财政部的李茜则生于1977年11月，毕业于山东财政学院财政专业，1998年参加工作。中共党员，硕士研究生学历，历任山东省费县挂职副县长，财政部国际司国际金融组织二处副处长、机关团委书记(副处级)等职，现任财政部机关团委书记(正处级)。对于李茜，官方如此评价：“毕业后一直在财政部工作，先后在农业司、国际司工作过，其间还在山东省费县挂职担任过县长助理和副县长，2007年5月任财政部机关团委书记。在工作中，能够较好的协调各种关系、有较强的创新意识和较高的工作热情。”<br />
<br />
　　周小棋和李茜昨天分别在大会上与大会组成人员见面。记者在现场看到，李茜身穿黑色西服，面容清秀，显得干练、大方，堪称难得一见的美女副市长。<br />
<br />
　　大会会议昨天还听取了《昆明市城乡规划条例(草案)》、《昆明市殡葬管理条例(修订草案)》、《昆明市档案条例(草案)》，以及《昆明历史文化名城保护条例》等众多报告、说明、议案。今日，大会还将召开第二次全体会议。 <br />
<br />
　(云南信息报)供稿</div>

							
							
							
							
