													<h2>佛罗里达生态住宅？豪宅？</h2>
						
						
																											<div id="postmessage_217944" class="t_msgfont">当地开发商称这栋面积约1,394平方米的房屋是“绿色”的。目前它的售价为2,900万美元。<br />
.<br />
<br />
<br />
<br />
<br />
<br />
.<br />
<br />
<br />
<br />
.<br />
<br />
<br />
　　<img src="http://chinese.wsj.com/pictures/photo/realestate090825/01.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　佛罗里达州开发商、房产经纪人麦金尼(Frank McKinney)在佛罗里达摩纳拉潘海滩南洋路620号建造了这栋被称作“生态住宅”的房屋。 <br />
<br />
<br />
<br />
　　<img src="http://chinese.wsj.com/pictures/photo/realestate090825/02.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　这栋面积约1394平方米、占地约6475平方米的住宅，其设计受到了塔希提岛、斐济、巴厘岛、夏威夷等地的发，目前市值2900万美元。麦金尼说，住宅被称作Acqua Liana，在塔希提语中是“水之花”的意思。 <br />
<br />
<br />
<br />
　　<img src="http://chinese.wsj.com/pictures/photo/realestate090825/03.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　房屋用竹子、柚木、椰树、棕榈树制成的再生木材做地板。麦金尼在该房产网站上说，其采用的可再生木材总共可以使超过42.5万平方米的雨林免于采伐。 <br />
<br />
<br />
<br />
　　<img src="http://chinese.wsj.com/pictures/photo/realestate090825/04.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　住宅拥有节能灯具、太阳能电池板和一个草坪车道，还在混凝土 和地基中加入了烧煤场的煤灰。 <br />
<br />
<br />
<br />
　　<img src="http://chinese.wsj.com/pictures/photo/realestate090825/05.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　房屋有7间卧室、11间浴室、2部电梯、2个洗衣间和一个家庭影院（如图）。 <br />
<br />
<br />
<br />
　　<img src="http://chinese.wsj.com/pictures/photo/realestate090825/06.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　画面中的地板由水鼓泡、玻璃和发光的LED灯制成。 <br />
<br />
<br />
<br />
　　<img src="http://chinese.wsj.com/pictures/photo/realestate090825/07.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　房子里的小吧台上还安置了装有2000加仑盐水的水族箱。 <br />
<br />
<br />
<br />
　　<img src="http://chinese.wsj.com/pictures/photo/realestate090825/08.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　户外的水中花园很适合游泳。 <br />
<br />
<br />
<br />
　　<img src="http://chinese.wsj.com/pictures/photo/realestate090825/09.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　游泳池还包括一个浮在水面上的阳光平台。这些泳池、池塘和瀑布使房子的温度降低了一些；麦金尼在房产的网站上写到，这样就节省了冷气的费用。 <br />
<br />
<br />
<br />
　　<img src="http://chinese.wsj.com/pictures/photo/realestate090825/10.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　这栋房子尚未获得美国绿色建筑协会(Green Building Council)颁发的“能源和环保设计认证(LEED)”，但是麦金尼希望能得到这项认证。LEED的顾问说，这座房子有点太大了。</div>

							
							
							
							
