													<h2>爱德华&#8226;肯尼迪生平</h2>
						
						
																											<div id="postmessage_217946" class="t_msgfont">.<br />
<br />
<br />
<br />
<br />
<br />
.<br />
<br />
<br />
<br />
.<br />
<br />
　　<br />
<br />
　　<img src="http://chinese.wsj.com/pictures/photo/TedKennedy/01.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　Terry Ashe/Time &amp; Life Pictures/Getty Images <br />
<br />
　　在被脑癌夺去生命后，泰德&#8226;肯尼迪成为了富有传奇色彩的肯尼迪四兄弟中唯一一位死于自然疾病的，也是唯一一位活到头发苍白才去世的。<br />
<br />
<br />
<br />
　　<img src="http://chinese.wsj.com/pictures/photo/TedKennedy/02.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　Bachrach/Getty Images <br />
<br />
　　爱德华&#8226;肯尼迪是九个孩子中最小的。照片为1930年肯尼迪一家在麻省布鲁克林合影。前排从左至右：小约瑟夫，母亲罗丝，罗伯特，爱德华，父亲大约瑟夫，帕特里夏，简。后排从左至右：尤妮丝，约翰，凯瑟琳，罗丝马丽。<br />
<br />
<br />
<br />
　　<img src="http://chinese.wsj.com/pictures/photo/TedKennedy/03.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　&nbsp;&nbsp;Alan Band/Keystone/Getty Images <br />
<br />
　　爱德华&#8226;肯尼迪（中）和兄弟罗伯特（左）、约翰&#8226;肯尼迪在1950年代合影。约翰&#8226;肯尼迪于1963年总统在任期间被刺杀，罗伯特于1968年参与民主党总统候选人提名竞选时被刺杀。<br />
<br />
<br />
<br />
　　<img src="http://chinese.wsj.com/pictures/photo/TedKennedy/04.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　John Loengard/Time &amp; Life Pictures/Getty Images <br />
<br />
　　爱德华&#8226;肯尼迪一生历经丑闻和灾祸，1969年还在查帕奎迪克岛附近遭遇车祸。他驾驶的汽车冲出大桥落水，车上乘客玛丽&#8226;乔&#8226;科佩奇尼(Mary Jo Kopechne)溺水身亡。照片摄于查帕奎迪克岛车祸之后不久。<br />
<br />
<br />
<br />
　　<img src="http://chinese.wsj.com/pictures/photo/TedKennedy/05.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　Michael Ochs Archives/Getty Images <br />
<br />
　　爱德华&#8226;肯尼迪1962年被选入参议院，作为美国历史上最年轻的参议院多数党党鞭，任期从1969年到1971年。之后将近47年的时间里他一直在参议院任职，成为美国参议院历史上在任时间第三长的参议员。<br />
<br />
<br />
<br />
　　<img src="http://chinese.wsj.com/pictures/photo/TedKennedy/06.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　Steven Clevenger/Getty Images <br />
<br />
　　爱德华&#8226;肯尼迪创立了影响数百万美国人的法律法规，涉及教育、医疗保障、移民、工资等一系列问题。<br />
<br />
　　左图为1971年爱德华&#8226;肯尼迪在一场游行中对越战老兵讲话，约翰&#8226;克里(John Kerry)（右下角）也站在人群中。<br />
<br />
<br />
<br />
　　<img src="http://chinese.wsj.com/pictures/photo/TedKennedy/07.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　CBS Photo Archive/Getty Images <br />
<br />
　　查帕奎迪克事件之后，爱德华&#8226;肯尼迪轻而易举地重新获选参议员，但是车祸给他的政治生涯造成重大打击，他决定不参与1972年和1976年的总统选举。<br />
<br />
　　左图中，肯尼迪在1976年美国民主党全国代表大会上接受哥伦比亚广播公司沃尔特&#8226;克朗凯特(Walter Cronkite)采访。<br />
<br />
<br />
<br />
　　<img src="http://chinese.wsj.com/pictures/photo/TedKennedy/08.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　Dirck Halstead/Liaison/Getty Images <br />
<br />
　　1980年，爱德华的政治生涯发生了飞跃，成为肯尼迪兄弟中第三个，也是最后一个参加总统竞选的。1980年爱德华退出了总统竞选，左图是民主党全国代表大会上爱德华退选后和他当时的妻子琼(Joan)。<br />
<br />
<br />
<br />
　　<img src="http://chinese.wsj.com/pictures/photo/TedKennedy/09.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　JOHN MOTTERN/AFP/Getty Images <br />
<br />
　　1983年，爱德华与弗吉尼亚&#8226;琼&#8226;贝内特(Virginia Joan Bennett)离婚。1992年与维多利亚&#8226;雷吉(Victoria Reggie)结婚。左图是他与妻子雷吉(中)、儿子小泰德(Ted Jr.)和女儿卡拉&#8226;安妮(Kara Anne)抵达玛莎葡萄园(Martha's Vineyard)与美国总统克林顿会面。<br />
<br />
<br />
<br />
　　<img src="http://chinese.wsj.com/pictures/photo/TedKennedy/10.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　Charles Tasnadi/Associated Press <br />
<br />
　　1988年，爱德华组织集会赢得公众对于针对南非种族隔离制度的制裁法案的支持。同年，他发起了在美国具有里程碑意义的《民权法案》，禁止联邦基金支持下的机构违反反歧视法。<br />
<br />
<br />
<br />
　　<img src="http://chinese.wsj.com/pictures/photo/TedKennedy/11.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　Ron Edmonds/Associated Press <br />
<br />
　　1994年，他的儿子帕特里克&#8226;肯尼迪(Patrick Kennedy，左)，作为罗得岛代表当选国会议员。<br />
<br />
　　今年3月，爱德华最后一次来到参议院。他拄着拐杖，并由他的儿子陪同，参加由他提出的50亿美元扩大公共服务的法案投票。<br />
<br />
<br />
<br />
　　<img src="http://chinese.wsj.com/pictures/photo/TedKennedy/12.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　Jeff Haynes/AFP/Getty Images <br />
<br />
　　2004年，爱德华为正在角逐总统宝座的马萨诸塞州参议员约翰&#8226;克里（图左）助选。<br />
<br />
<br />
<br />
<br />
　　<img src="http://chinese.wsj.com/pictures/photo/TedKennedy/13.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　Evan Vucci/Associated Press <br />
<br />
　　2008年，爱德华在巴拉克&#8226;奥巴马(Barack Obama)竞选总统的关键时刻给予支持，把他与肯尼迪总统相提并论，称其为“一个有着非凡领导能力及优秀品质的人。”<br />
<br />
<br />
<br />
　　<img src="http://chinese.wsj.com/pictures/photo/TedKennedy/14.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　Stan Honda/AFP/Getty Images <br />
<br />
　　这位马萨诸塞州参议员在2008年5月被诊断出脑部患有恶性肿瘤。图为在接受治疗后，肯尼迪于2008年8月召开的美国民主党全国代表大会上发表演说。</div>

							
							
							
							
