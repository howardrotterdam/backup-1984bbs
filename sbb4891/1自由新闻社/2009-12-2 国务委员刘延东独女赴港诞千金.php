<br />
【本报讯】近年内地孕妇蜂拥来港分娩，为的是高水平医疗服务及子女拥有居港权，原来连国家领导人女儿也不「执输」。 《多维月刊》报道，中央政治局委员、国务委员刘延东的独生女，今年4月初在香港港安医院诞下一名千金，刘延东为看孙女一面而来港，特首曾荫权更在礼宾府宴请了刘延东两母女。<br />
<br />
4月时刘来港看孙女<br />
<br />
今年4月23日分管港澳事务的刘延东非正式访港，曾私下与特首曾荫权会面，原来刘延东当时有更重要「任务」。据《多维月刊》报道，刘延东的女儿杨帆及女婿冯琦在港经商，今年4月初杨帆在香港港安医院诞下女儿，荣升外婆的刘延东完成美国访问后，本应紧接飞往新加坡，但临时决定来港转机，目的是抽空探望女儿及看望还未满月的外孙女。据报当时还没坐完月的杨帆，带同女儿及夫家亲戚，到刘延东下榻的港岛香格里拉酒店共叙天伦。<br />
<br />
刘延东在港逗留不足一天，曾荫权在礼宾府设午宴招待刘延东、同行的还有教育部长周济及科技部副部长李学勇，据悉杨帆也是座上客。 《多维月刊》指，港府获悉杨帆在港产女后，立即搜集这位太子党的资料存档。刘延东其后将新加坡访问，由原来四日缩短至一日半。<br />
<br />
特首办昨晚回应指，需时间了解及查证上述时间特首是否曾与刘延东会面。<br />
<br />
反映本港医疗质素高<br />
<br />
原任统战部长的刘延东升至国务委员后，属国家领导人级数，她将于本周五来港主持翌日的东亚运开幕礼。<br />
<br />
中文大学妇产科学系系主任钟国衡表示，近年来港产子的内地孕妇来自不同阶层，有些非常富有，「医疗费唔系问题」；有些则为取得较好服务而倾尽家财来港，证明本港医疗水平很高，令内地人有信心来港分娩。内地夫妇也希望子女有香港居留权，方便日后出国。时事评论员刘锐绍表示，国家领导人家属来港产子反映本港医疗质素高，政府可利用优势发展产业。<br />
<br />
