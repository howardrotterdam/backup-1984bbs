													<h2>D一篇：把产妇和婴儿当小白鼠做实验</h2>
						
						
																											<div id="postmessage_351469" class="t_msgfont">D一篇，不一定是t院^e，懂行的人cu一下<br />
<br />
史上最牛妇幼保健院：把产妇和婴儿当小白鼠做实验<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;记录本人及幼女在上海市国际妇幼保健院从孕检剖宫产至今的凄惨生活<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;刘建<br />
<br />
&nbsp;&nbsp;我是一名安徽省金寨县来沪务工人员，2009年春节后发现怀孕，到上海市国际妇幼保健院建立产检联系卡，并积极配合医生做产前所有的检查和筛查，所有结果显示是一对健康胎儿。双胞胎对于我们家来说就是上帝的恩赐，此后我每天都愉快地孕育着这一对生命。<br />
<br />
 <br />
<br />
噩运在我认为最可靠的医院降临<br />
<br />
&nbsp; &nbsp; 2009年7月13日我在正常产检时，尿样检查出蛋白指标高，门诊医生要求我住院治疗，我办了入院手续，住进产科8层病房，下午B超检查时，当时医生交头结耳半天，后又找来一位医生才写出结果报告。回到病房后不久，楼层主任毛医生就找我谈话，要求我马上剖宫产，说B超显示一胎脐带血流“无穷大”，会随时发生死亡，当时我和老公考虑孩子太小，既然住进了这么高级的医院里，胎儿监护设施这么好，有突发情况时，再剖也来得急，我们就在医嘱单上签下“拒绝剖宫产，后果自负”，这几个字竟成了我和老公每天必签的作业了。每天做完B超检查，医生就会催促我剖宫产。<br />
<br />
&nbsp;&nbsp;孩子在我腹中我感觉到她们很好，可我不知为什么B超会是这个结果，受不了医生的催促，我于20日办理了出院，回家休养了一周。27日是常规检查日子，我挂的是专家门诊，专家看到我感到非常惊奇，她说：“从你出院，楼层医生就说你这个患者很危险，胎儿可能会死在腹中，今天听到胎心两个都很正常，要真是脐带血像B超反映不正常的话，这两个胎儿早就没了，可现在还活着，真是无法解释。到时一定要把胎盘和脐带检查一下，也算是个医学特例了。”<br />
<br />
&nbsp;&nbsp;因当天检查结果尿蛋白依然偏高，门诊专家医生建议我再住院观察，我当时说怕入院后楼层医生还要催促我剖宫产，门诊专家医生说她会通知楼层医生不要那么早给剖出来，住进医院怎么也要比在家里有安全保障，这样我才又放心的住进医院，可是第二天，毛医生又开始催促我剖宫产，又要每天在医嘱单上签写“拒绝剖宫产，后果自负”的免责单。<br />
<br />
 <br />
<br />
&nbsp;&nbsp;这样又拖到7月31日下午，医生让我老公过来谈话，要求晚上加班给我做剖宫产，我们每天被医生催促得实在没有办法，我还要听他们讲听不懂但很怕人的医学术语，什么胎死腹中、双胎都保不住，死胎对孕妇产生毒素等等吓人的话。我们还是拒绝当天剖宫产，我当晚被转到产房监护。8月1日早9点我做完B超，医生叫我马上打电话给家属，让过来签字实行剖宫产，我们实在承受不了这样的心理压力，才下了决定在剖宫产同意书上签了字，并于当日上午11时40分剖出一对提前53天的挛生女儿。出生后两个女儿健康评分满分为10分，1分钟为9分和8分；5分钟为10分和10分。胎儿脐带和胎盘被院方留下做检验，当时说二周会给检验结果，这样就能解释B超检查时一胎脐血流为什么“无穷大”。两个女儿于出生后就被送入该院的新生儿科病房，交了40000元押金。<br />
<br />
 <br />
<br />
疑问：<br />
<br />
1．产科医生，你们为了满足自已的好奇心和求知欲，非要把B超显示脐血流不正常的脐带早些天取出来研究研究，就把我给推上手术台，剥夺了我的孩子的正常孕育权利，你们是这之后所有矛盾纠纷和不幸的缔造者。<br />
<br />
2．产前检查B超显示脐带血异常，可产后的脐带和胎盘检验报告的结果是：脐带未见异常！！！我想问医生，是B超机有问题还是你们医生的主观意断造成了我的早产？<br />
<br />
3．8月28日，我去医院找毛医生询问胎盘和脐带的检验结果及孩子感染的情况，可是毛医生对我说：“谁让你这么早剖出来？”我不知道当初住院时，是谁在天天催促我，要给我进行剖宫产。给我剖宫产的目地是真的帮我和孩子，还是另有所图。<br />
<br />
 <br />
<br />
这是产前的B超检验报告<br />
<br />
 <br />
<br />
这是我产前每天要签的医嘱单<br />
<br />
&nbsp;&nbsp;<br />
<br />
 <br />
<br />
这是胎盘脐带的检验报告<br />
<br />
 <br />
<br />
 <br />
<br />
这是剖宫产和出生健康评分记录<br />
<br />
 <br />
<br />
 <br />
<br />
 <br />
<br />
噩运没有结束，噩运又降临到女儿身上<br />
<br />
&nbsp;&nbsp;我产后第六天出院回家休养，医院不让见孩子，每天只能询问新生儿科医生两个女儿情况，每次医生都说情况很好。8月20日医生打电话说小女儿发生感染，叫我去医院签字才能使用抗生素治疗，我到医院后医生让我签了两面同意用药单，说要是先使用头胞类抗生素效果不好的话会马上使用高级的抗生素，就不用家属多跑一趟了，这句话当时很让我感动（后来才知道，医生在明知使用药物效果不好，却一直用到9月4日，也没有换药治疗）。<br />
<br />
&nbsp;&nbsp;8月25日我打电话询问孩子病情，医生说孩子已经出现败血病症状，8月28日我要求复印病历把孩子转院治疗，医生说得预约到9月4日复印病历，并承诺说他们有把握治疗。9月18日医生打来电话说孩子肝功能出现问题，我老公到医院按照医生的口诉要求，在医嘱单上签了一份拒绝转院同意留院治疗的证明，我们认为孩子从当初感染到发展到肝功能损害，医院都负有责任的。并且医院对我们没有告知病情的严重程度。<br />
<br />
&nbsp;&nbsp;9月23日一早医院给我们下了病危通知书，我们到医院，医生让我考虑10分钟，是转院还是接回家等死，任何一位母亲都会选择转院治疗吧。转院的相应手续全是医生操办的，现在回想起来，这就是把我们推出去好为推拖责任提供便利的手段。<br />
<br />
 <br />
<br />
&nbsp;&nbsp;孩子是在9月23日转入上海儿科医院，当时入院检验的结果肝功能损害非常严重，我们一直见不到孩子，电话询问病情，医生说是由重度感染造成的，我的孩子是同卵双生的双胞胎，出生时的生理指标均良好，大女儿于8月31日出院回家喂养，现况很好。孩子出生后在国际妇幼新生儿科一天花费几千元，孩子出生后20天才发生感染，这一定是外源性，是护理不当造成的，肝功能损坏也是发生在感染之后在治疗过程中发生的，国际妇幼保健院新生儿科是有责任的。<br />
<br />
疑问：<br />
<br />
1.发生感染后，在治疗过程中已发现药物效果不好，为什么不更换药品？<br />
<br />
2.什么原因发生的感染，在哪道环节发生的，为什么会发展成肝功能损害？<br />
<br />
3.新生儿科主任施医生说，新生儿感染率国家允许44%，美国21%。别的孩子都好好的，就你的孩子感染，你的孩子体重轻（言下之意就是我的孩子感染了活该），体重轻也是你们医生给我早产造成的！！！<br />
<br />
 <br />
<br />
我可以一死了之吗？？？？？<br />
<br />
 <br />
<br />
 <br />
<br />
 <br />
<br />
看着女儿这样，我作为一个母亲，非常痛心，相信做过母亲，父亲的人都有这种切肤体会!<br />
<br />
 <br />
<br />
悲情上访路, 从上海到北京又到上海，路在何方？<br />
<br />
&nbsp;&nbsp;同院方交涉几次，答复我们说她们没有责任，我9月25日去上海市徐汇区法院询问相关事情处理办法，法院办事人员让我找法律援助部门，法律援助部门告知我说打官司风险大、时间长，还是让我去找医院交涉，我电话询问上海市卫生局，卫生局办事人员说只有等国庆节过后处理。<br />
<br />
&nbsp;&nbsp;没有办法，我于10月9日到国家卫生部上访求助，卫生部接待人员给我复函让我回上海找上海市卫生局，上海市卫生局回复我还让我去找这家医院解决，我给上海市韩正市长写信求助，结果也是让我找这家医院。我转了一圈又回到了开始的地方，我不知道还能找哪个部门。<br />
<br />
&nbsp;&nbsp;现在小女儿还躺在上海复旦儿科医院的病床上，同卵双生的孩子原本差别不会很大，但是现在大家看一下，百天照对比，小女儿被病情折磨得哪还有个人样？现在国际妇幼保健院推脱责任，上海市儿科医院又让我们接孩子回家喂养，询问孩子现在病情怎么样了，医生也只是说肝功能在恢复好转，也不给我说到底能恢复到什么程度，是可以康复还是暂时好转？怎样鉴定由谁来给我女儿鉴定？儿科医院为什么现在急着催我接回孩子？哪里是我申诉的地方？10月16日我写好遗书，来到上海国际妇幼保健院，想从楼上跳下去一死了之，被边上好心人救下来，我不知道要是当时我死了，会不会有相关部门和人来出面解决问题。生活在上海这个大都市中，我们外来人向哪里求助？我真正体会到国际大都市的残忍、无助、绝望……<br />
<br />
 <br />
<br />
 <br />
<br />
 <br />
<br />
 <br />
<br />
 <br />
<br />
这是上海市长韩正回复的<br />
<br />
父母官的答复，我能找的，我都找了，就只剩下天上的玉皇大帝没找了，只是我不知道路在何方？<br />
<br />
 <br />
<br />
 <br />
<br />
 <br />
<br />
难道，就因为我是普通老百姓，是外来务工人员，我们就是你们医生的试验品？就是你们的小白鼠吗？<br />
<br />
我要告诉你，我们是普通人，经不起你们的实验！！！<br />
<br />
还我健康的女儿......<br />
<br />
天苍苍，野茫茫，何处话凄凉......<br />
<br />
谁能告诉我，作为一个老百姓，我该怎么办？？？<br />
<br />
求天，求地，求神，求佛，求官，求医，求人，求己...我还能求谁？<br />
