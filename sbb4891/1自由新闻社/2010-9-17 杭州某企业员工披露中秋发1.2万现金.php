													<h2>杭州某企业员工披露中秋发1.2万现金</h2>
						
						
																											<div id="postmessage_834036" class="t_msgfont">杭州某企业员工曝中秋发1.2万现金 网友称嫉妒<br />
 来源：浙江在线 2010年09月17日<br />
[提要] 连日来，关于“史上最复杂放假月” 的话题还在热议，又有新爆炸性消息流传：“杭州某单位正式工中秋福利：1.8万元，非正式工福利：3000元。”另一家公司更爆出除开有1.2万现金外，还有8000元旅行奖金+8000元杭大购物卡+3000元月饼+一箱红酒+一台苹果MacBook Air(笔记本电脑)等等，并称是一家世界500强的石油公司。[为何继网上晒工资条后，又晒福利？]&nbsp;&nbsp;<br />
 <br />
金饭碗？ <br />
<br />
 <br />
 <br />
2010年七大节日休假示意 搜狐新闻制图 <br />
<br />
<br />
　　浙江在线09月17日讯 连日来，关于“史上最复杂放假月”的话题还在热议，又有新的爆炸性消息在杭州流传。民间流传最广的是“某单位正式工中秋福利：1.8万元，非正式工福利：3000元。”<br />
<br />
　　这则消息被网友在杭州当地著名BBS曝出后，随之而来的口水已经蔓延了大半个杭州城。有人感慨：好公司一笔福利费竟然比普通工人一年的收入还高？<br />
<br />
　　为何继网上晒工资条后，网友又纷纷上网晒福利？网上晒福利，是公司行为还是个人行动？这么曝光，会刺激职场中的“杜拉拉”勇敢向前冲吗？究竟是什么样的心态，让人愿意这么晒隐私？杭州本土畅销书作家、《潜伏在办公室》的作者陆琪老师有话要说。<br />
<br />
　　民间福利<br />
<br />
　　“妖股”：一笔福利赛过普通工人年收入<br />
<br />
　　“正式工中秋福利：1.8万元，非正式工福利：3000元。”如果听到这个单位的中秋福利，你已经张大了眼，那么接下来另一家公司的丰厚待遇，恐怕会让你惊叫―― “我们单位今年的双节福利，中秋节：1W2(即1.2万元)现金+8000元杭大购物卡+3000元月饼券+一箱红酒+2箱水果，国庆节：8000元的旅行奖金+一台苹果MacBook Air(笔记本电脑)。――我们是一家世界500强的石油公司，具体公司名字就不说了……”<br />
<br />
　　对此，有网友在微博上“怨念”，“我心里极度不平衡，我嫉妒，我妒忌”。<br />
<br />
　　“滞涨股”：十年如一日，怎么跑得赢CPI？<br />
<br />
　　杭州本地一著名论坛上，网友“恋错了床”如此记录道――<br />
<br />
　　“又是中秋节，很多单位都开始发福利。当然我们单位也不例外，说到我们的福利，就是十年如一日。从公司有中秋福利开始，我们的标准就是100元，应该保持了6-7年了吧。在这个物价大涨的年代，青菜从0.5元到了3.5元，大米从1元到了3元，榨菜小月饼都从0.5元到了2.5元了。而我们的福利却一直保持不变，真是佩服我们的老板。看着身边朋友们每年福利翻倍地增长，只能叹息自己入错行，选错老板。”<br />
<br />
　　这样的滞涨，只能让人发出一声叹息。<br />
<br />
　　“成长股”：外企总算入乡随俗了<br />
<br />
　　记者还看到一个落寞的帖子，《杭州的外企那么多，怎么没人来分享一下啊？》。发帖网友是一家外企的员工。<br />
<br />
　　他自称进公司的头两年，中秋福利啥都没有。想想人家外国人，肯定是不过什么中秋节的，也表示理解，于是进超市买了月饼，过自己的节！后来，不知哪个中方领导进言，前两年公司居然有中秋福利了。同事们感动得不知说啥好了――(老外)终于知道咱们中国人是过中秋节的了！虽然那福利只是节日当天午餐时，公司食堂发两个“咪咪小的月饼”。要知道，那时的中秋还不是法定假日。<br />
<br />
　　“再后来，又不知经哪些领导研究决定，咱们的中秋福利升级了――100元购物卡！相当感动――尽管物价飞涨，但100元还可以买一大包小月饼滴！呵呵，说完了，总的来说，咱的中秋福利是越来越好了啊！”从无到有，这些“成长股”让员工心生感激。<br />
<br />
　　职场调查：真金白银永远最现实<br />
<br />
　　福利，是求职者时时刻刻关心的民生问题，也是在职者永远晒不完的职场话题。如今，晒福利的职场人士，在BBS，在论坛，在微博客上愈来愈多。随着城市半径变大，CPI指数走高，被晒的从工资，扩展到过节费，甚至是车贴、饭贴等细枝末节的费用。许多知名企业的HR总监感觉生活在玻璃房里，透明度极高。<br />
<br />
　　员工挑选公司最为看重的因素是什么？员工跳槽意愿中最关键的因素是什么？员工离职的最重要原因是什么？根据前程无忧的历年调查显示，“薪资福利”也许不是时时排名第一的选项，但绝对是时时刻刻萦绕在员工心头的一个重要参考因素。如同企业在衡量员工的绩效时会想法设法地用数字来量化一样，员工在衡量企业时，最终也会将他们所看重的职业发展、公司规模、企业文化等等因素折算为数字――真金白银永远最现实。<br />
<br />
　　前程无忧近期特别针对车贴、饭贴的问题做了网上调查。关于车贴的调查结果显示，有53%的受访者表示自己所在的公司既不提供免费班车也不提供车贴，只有12%的白领每月能够享受到100―300元的交通补贴，另有13%的受访者只能享受到公司班车，却没有任何现金补贴。而在饭贴这个问题上，35%的白领每天只能享受10元以下的餐补，还有26%的白领所在的公司既不提供公司盒饭，也不提供饭贴，午餐全靠自己买单。可见，即便是车贴、饭贴，也不是人人都平等的。<br />
<br />
　　分析：“晒”的背后，更值得关注的是炫富心态<br />
<br />
　　《潜伏在办公室》系列热卖，作者陆琪对那些晒高额福利的职场中人做了点评。<br />
<br />
　　虽已成为名人，有了助理的陆老师在电话里听到那笔1.8万元的丰厚福利时，还是惊叫了出来。但他觉得，发多少很正常，只要企业自己拿得出，大家不用反应过激。“这个社会就是有竞争，有等级关系的；有老板，有高管，有员工。如果大家均贫富，就没有动力了。”聊到这，不免让人想起当年那个幸福的“天下第一村”。陆老师说，华西村的模式在初期是好的，但是后期会失去竞争力，失去变化。没有欲望，动力就会越来越不足。<br />
<br />
　　让陆琪关注的是，那些上网来晒幸福的人的心态很奇怪，他们对此有优越感。对那些收入高的单位员工而言，1.8万元在工资中所占的比例并不高，1.8万元在杭州连新房的1个平方米都买不到。但中秋福利拿到这笔钱，连发帖人自己都觉得有点“离谱”。这表明，他从前是没有拿到过这么多的。这几年国有企业的配薪方式在发生改变。原来人们印象中的“金饭碗”银行、保险、证券公司的员工拿到的钱没有从前多了，而一些新兴行业(如通讯公司)和一些民营企业的钱发的越来越多了。<br />
<br />
　　再者，那些晒幸福的人可能在其他方面没有优越感。当今社会上人们关注的有两类人，一是富人，二是教师、医生等令人尊重的职业。而那些普通的白领，他们的社会地位不高，也没有别的好炫耀。怎么表达他比你厉害？于是就有了晒一族。<br />
<br />
　　也有人说这样的晒工资、晒福利，是公司HR(人力资源部门)的行销手段，并非单纯的个人行为。陆琪说，这样的传播，对于发放高福利的单位是最有利的，因为它会产生很大的刺激动力。动力是欲望。年轻人看到这些高福利，就会自我激励“我要向上爬”，动力非常足。</div>

							
							
							
							
