<br />
&nbsp; &nbsp; 管碧玲表示，前国安会秘书长苏起接受媒体专访，才指马政府在两岸有不能公开的秘密沟通管道，将马英九的「行动」曝光，王晓波则将马英九的「思想」完整地公诸于世，马对王的「代言」真假，有必要清楚表态。<br />
<br />
