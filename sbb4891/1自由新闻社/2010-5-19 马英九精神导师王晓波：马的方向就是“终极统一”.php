<br />
&nbsp; &nbsp; 自称被马英九总统视为「大哥哥」的文化大学哲学系教授王晓波，日前接受中国媒体访问，针对如何评价马总统上台后表现，他表示，「终极统一」就是马英九的方向，两岸签署和平协议也是马英九的方向。<br />
