																											<div id="postmessage_624381" class="t_msgfont">《崇祯并发症：自恋型领袖的误国》<br />
文：南方朔<br />
<br />
北京故宫的后门有小丘，叫做煤山。上有当年崇祯皇帝自缢的那棵树，原树兵S，后人重植，已枝叶扶疏，成了大树。多年前一个秋日黄昏，我到该处凭吊，绕树三匝，古木悲风，诉说的都是历史的凄怆和反覆的愚蠢。<br />
在此重提明朝崇祯皇帝，并无任何借古讽今的念头。而是用当代政治学的标准来分析，崇祯乃是所谓「自恋型领袖」的最标准样本。领袖的极端自恋，小则误己误身，大则误国误民，崇祯的自恋，就是个「误」，他「误」了一切。<br />
<br />
&nbsp; &nbsp; 崇祯乃是典型的误国亡国之君。可是他即位之初不是这样的。当时魏忠贤滥权，朋党营私，崇祯立即杀魏忠贤并全面罢黜他的党羽，看起来很有一点中兴气象，崇 祯也自己照镜子，愈看愈得意，真的以为自己是盖世无双的明君。<br />
<br />
&nbsp; &nbsp; 于是由自恋转自大，由自大变成刚愎自用，刻薄寡恩。明末出了一堆混蛋皇帝及大臣，但他们再怎 么混蛋，还是知道要替国家留一些能吏勇将，去做他们没有能力去做的事。但自恋刻薄的崇祯自以为是，认为天下只有他是对的，别的人都不尽忠报国，于是他连国 家最后的名将熊廷弼、袁崇焕这种人都敢杀。他在位十七年，只相信自己和身边一群新的奸臣小人，搞到国事日非，民生更苦，最后是贫苦农民造反所形成的流寇， 在李自成率领下攻入北京。<br />
<br />
&nbsp; &nbsp; 最荒谬的是，崇祯到了最后还不认为亡国是他的责任！他自缢煤山之前，在衣襟写了遗诏，仍有「然皆诸臣之误朕也」之句。自己把天下 搞垮，还以为与他无关，都是别人的事。这种混蛋皇帝，真是自恋到了疯狂昏z的极致！<br />
<br />
&nbsp; &nbsp; 崇祯皇帝自恋自大自以为是，乃是自恋型的领袖走向疯狂的极端代表。近代政治学对领袖的自恋人格着墨极多。一般而言，领袖有适度的自恋，把自恋转化成自尊 自重以及催化出的自我能力的严格要求，这未尝不是好事，但领袖病态的自恋却也所在多有。那种领袖只爱自己，不爱任何他以外的别人，永远活在自我的良好感觉 里，相信自己永远不会错，责任都在别人。当一个国家出了这种自恋型的领袖，老百姓只有「挫咧等」的份了。<br />
<br />
&nbsp; &nbsp; 当代知名的领导学专家波耶特（Joseph H.Boyett）在近着《选民进化论》（Won’t Get Fooled Again）里，有一个专章谈自恋型领袖。他指出，自恋型领袖在达到权力的高峰前，由于自恋所创造出的形象很迷人，而且自恋的负面效果还没有累积到足够的 量，人们普遍会对自恋型领袖寄予过高的期望，因而有利于他快速攀上权力高峰。但到了这时，自恋型领袖的人格及能力特质里的巨大缺点就会开始暴露，而使他站 到很陡峭的滑坡边缘，很容易快速下坠，波耶特还特别条列出自恋型领袖的许多负面领导症状，我在此将其中比较有现实性的若干缺点摘要列出：<br />
<br />
