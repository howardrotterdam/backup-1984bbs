陈国军<br />
（《多S月刊》2009年11月）<br />
北京中南海最近正悄悄为中共总书记兼国家主席胡锦涛转型，以符合中共领袖的法统形象，或许也可以借此弥补一下合法性不足的问题，告诉中国的老百姓和世界，胡锦涛是类出天授的国家领袖。<br />
转型首先从头上的发型开始。敏锐的读者和观众最近一定会发现，在电视镜头中和官方新闻照片上出现的胡锦涛，形象似乎比以前顺眼，发型已经没有以前那样夸张，原来的三七开已经不那么明显，最重要的是不再是那么蓬松的飞机头，头发开始向后梳。<br />
