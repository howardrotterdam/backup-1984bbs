<br />
向后梳的背头是中共领袖的特征。毛泽东的背头形象曾是这位“中国人民大救星”的一个重要标志，任何稍懂画画的人，只要画个圆圈，再勾出一个背头的线条，毛主席的脑袋就出来了，就成了亿万中国人顶礼膜拜的神像。<br />
毛泽东以后的历代中共领袖，从华国锋、邓小平和江泽民，不管之前是什么形象，一旦掌握了中国的皇权，马上在中南海化妆师的打扮下，顿时都是一个大背头，不管他以前是什么样的头型和发型。<br />
最典型的是华国锋。从湖南地方官到中央大员，包括国务院总理任上，一直留着小平头，加上圆脸上憨憨的表情，给人一个敦厚老实的形象，连“伟大领袖”毛主席也被他的形象征服，对他信任有加，给他写下“你办事，我放心”的遗嘱，最后将政权交给他。但一转眼间，为了适合领袖的法统形象，华国锋的小平头变成了大背头：头发留长点，加上发蜡一打，鬓角一修，“英明领袖”的形象即刻显现。<br />
<br />
之后的邓小平也是在成了中共实质上的最高领导人之后，把以前的小平头改成了背头。有人说是因为小平年纪大了，头发少了，稀疏了，所以才改的发型。但实际上，这都不是理由的理由，真正的原因其实还是法统的需要，统治心理的需要，毕竟是国家领袖，留个小平头显得多么不严肃。<br />
<br />
邓小平指定的中共第三代领袖江泽民显然更明白背头发型的重要意义，不但平时注意头发的梳理，即使是在出国进行国事访问的公众场合，他也毫不犹豫掏出梳子，梳理被风吹乱了的头发，保证了背头的整齐光亮，捍卫了中共领袖形象的法统。<br />
<br />
比起前几位中共领袖，今年六十七岁的胡锦涛头发多且黑，但其三七开飞机头发型却给人刻板拘谨的印象。因此，在登上权力顶峰多年之后，中南海的形象设计师开始调整这位中共最高领袖的形象，要恢复中共领袖形象的法统，要在不知不觉中实现胡锦涛形象的转型。<br />
<br />
一个略微背头的胡锦涛终于开始出现在公众的视野。按照中南海形象设计师的策略，这个转型必须是不知不觉的，不能太过突然，否则民众会大吃一惊，难以接受，不利于和谐稳定。于是，在设计好的步骤中，一个新的背头领袖形象正在出现，从略背到大背，从国内到国际，先是在天安门的国庆阅兵亮相，一个背头的、穿着中山装的中共总书记、中央军委主席出现在天安门城楼，接受万众的欢呼，接受军队的致敬，再到新华社在之后举办的国际传媒大会，会见国际传媒大亨，展示新的形象，也恢复中共领袖的形象法统。</div>

							
							
							
							
