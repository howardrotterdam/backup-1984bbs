																											<div id="postmessage_274616" class="t_msgfont">10月7日上午，胡锦涛考察北京国庆安保、交通和旅游工作，乘坐北京地铁4号线赴颐和园考察旅游情况。在颐和园考察途中，胡锦涛与各界群众亲切交谈，称“我们都希望国家富强、人民富裕，让我们一起继续来打拼！”（10月8日《广州日报》）<br />
<br />
　　胡锦涛的这句“让我们一起继续来打拼！”无疑是老百姓常说的口语。这让人想起了纪念党的十一届三中全会召开30周年的大会上，胡锦涛总书记的“不折腾” 三个字刚落音，人民大会堂听众席随即传来会心的笑声。显然，中央领导对语言“平民化”的坚持，显示对推动政坛“白话新风”，已然锲而不舍。<br />
<br />
　　当然，现在仍有官员喜欢沿袭所谓的习惯思维，似乎身为官员，就理应讲官话，讲大白话会让人觉得“不成体统”。那满嘴的官话，云山雾罩，似乎远离尘世，不近人情，失去了本真的面目，以致让人误以为官员几乎是一个模子刻出来的，程式化的言行举止，透出一股官风官仪，威严有余，亲近不足，而不可亲，也就难以可信，长此以往，官民之间，怕难以避免有隔阂和距离。<br />
<br />
　　也有官员以为唯有讲“官话”，才能显示出自己的水平。其实，民间的大白话远比官话精彩。或许，大白话不如官话那样滴水不漏地严谨，但因为直接来自民众，显然比官话更准确地反映民意。而因为语言的贴近百姓，也让人倍感亲切，而因为亲切，才激荡人心，由此产生无穷的力量。<br />
<br />
　　其实，官员本来就来自百姓，生活在现实生活之中，他们理应至情至性，和老百姓说同样的话，用同样的语言表达自己的喜怒哀乐。现在不少官员习惯于官话，是因为讲官话“保险”，虽然因此缺乏感染力，甚至说了也白说，既空耗自己的精力，也浪费人家的时间，却依然人云亦云，说到底，是缺乏责任意识。现在我们的国家在进步，个性官员越来越多，“要讲大白话”从来没有在为官之道中这么被强调过。早有分析人士指出：“这些含义丰富、口语化十足的语言，是中国民主政治在语言层面的一个进步。”而中央领导对推动语言平民化的身体力行，也为各级官员树立了一个榜样。 （浙江 钱夙伟）<br />
