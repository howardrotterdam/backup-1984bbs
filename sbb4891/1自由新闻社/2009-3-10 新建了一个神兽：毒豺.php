													<h2>江湖惊现又一神兽---毒豺</h2>
						
						
																											<div id="postmessage_50004" class="t_msgfont"><a href="http://www.hudong.com/wiki/%E6%AF%92%E8%B1%BA" target="_blank">http://www.hudong.com/wiki/%E6%AF%92%E8%B1%BA</a><br />
<br />
毒豺 学名：dictatorship <br />
种类：肉食者<br />
物种：犬类 <br />
<img src="http://a1.att.hudong.com/35/12/01300000354901123668129703565_s.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
发源地<br />
<br />
毒豺原产于中国，其历史悠久，已不可考，曾经遍布全世界， 如今仍活跃在中国，古巴，苏丹，朝鲜等地。 本世纪40年代，毒豺繁殖迅速，险些造成其他物种的整体灭绝。其危害性，可见一斑。<br />
<br />
习性<br />
<br />
毒豺是肉食动物，胃口颇杂，无所不食，而且爪尖齿利，天性好斗，不过这些都不足为奇，另人费解的是它们在吃掉其他动物之前都会围着它转上几圈，嘴里叽哩咕噜好一阵，貌似在表达什么，据动物学家翻译那意思是：“一切都是为了你好！”<br />
<br />
毒豺的习性十分奇特，由于耳膜天生异常薄弱，所以毒宀幌不短别的动物大声鸣叫，遇到随便叫唤的动物，毒豺就会冲上去将它们撕成碎片，动物们逐渐不敢出声，只会点头和鼓掌，终于造成了万马齐喑的局面。不过据专家研究毒豺们并不是完全听不得，它们其实喜欢听一种乐器的声音，这种乐器叫“苇咣筝”，每听到” 苇咣筝 “的声音，毒豺们都会面露喜色，手舞足蹈。迫于毒宓囊威，动物们都学会了演奏”苇咣筝“，据说在中国境内有无数的高手，其演奏技艺已达到出神入化的境界。<br />
<img src="http://a0.att.hudong.com/67/13/01300000354901123668131285905_s.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
天敌<br />
<br />
毒豺的天敌是鸣猪，鸣猪原产于西方，专以毒豺为食，近百年来繁殖迅速，遍布全球，毒豺逐渐被消灭，据说斗争中毒豺们背朝敌人，从屁眼里喷出一股股奇臭剧毒的液体，这种毒汁腐蚀性极强，沾者立刻血肉消融，只剩一架白骨，十分人！毒豺当年正是以此横行天下的，所过之处尸横遍野白骨森森，天下生灵为之涂炭....直到出现了鸣猪，鸣猪让毒豺们胆战心惊的地方就在于他们的嗓子，这也是”鸣猪“姓名的由来。鸣猪遇见毒豺时会发出响彻云霄的鸣叫声，穿透力极强，毒豺的耳膜承受不住，会七窍出血，气绝而亡。<br />
<br />
鸣猪的出现让动物们终于从毒豺的爪牙下解脱出来，它们重新恢复了欢快的鸣叫<br />
不过在毒豺土生土长的中国，那儿仍然是它们的天堂，不同于其他的毒澹在体形上它们通常要比普通毒宕罅奖兑陨希是属于毒逯掷嘀械摹按蠖濉保生活在此处不仅有毒豺，还有蚣蟾}，河蟹，鹳狸猿等等，因为它们都要依赖着毒豺生活，所以每当鸣猪到来的时候，河蟹和鹳狸猿们都会冲在最前面，鸣猪的嗓子虽然可以制服毒豺，却在河蟹的大钳子和鹳狸猿的爪子面前无能为力。一时间竟成了僵持局面，而生活在此处的动物们仍然不敢发出半点声音。<br />
<br />
 今年两会中，猥猿长就明确指出“毒豺是我们的衣食父母，而鸣猪是西方的物种，我国要在蚣蟾}的领导下继续坚决保护毒豺，抵御鸣猪！这个原则不能动摇！“<br />
