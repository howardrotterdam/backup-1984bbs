																											<div id="postmessage_11700" class="t_msgfont">他妈的，他们呢<br />
<br />
<br />
周晶波 | 涂鸦文字 | 出处：原创-IT| 2005年09月28日 11:17 | 阅读 0 次<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;下图：柴玲，右边摄于柴玲1989年5月27日19r<br />
<br />
参考：<br />
[一百零五人名单：柴/玲]北京师范大学研究生，天/安/门广场指挥部总指挥。&quot;六/四&quot;后遭通缉，并成功出逃中国。一九九─年入读美国普林斯顿大学硕士研究生。现在美国波士顿从商。<br />
<br />
[一百零五人名单：吾/尔/开/希]北京师范大学一年级学生，&quot;高/自/联&quot;负责人之一。&quot;六/四&quot;后遭通缉。先后流/亡法国、美国，现在台湾定居。<br />
<br />
[一百零五人名单：王/丹]北京大学历史系一年级学生，&quot;高/自/联&quot;负责人。&quot;六/四&quot;后遭通缉并被捕，被判/刑七年。一九九八年赴美&quot;保外就医&quot;，现为哈佛大学历/史/系学生。<br />
<br />
───────────────────────────────────────<br />
<br />
至今记得一个在美国搞了十多年民运的人说的一句话(大意)：海外民运，这么多年基本是破产的。<br />
<br />
----没等共产党搞他们，民运阵线自己早乱成一锅粥了，为了什么？为了争权夺利、霸占山头、拉帮结派、抢占主义高地，闹得不可开交。<br />
<br />
离开柏杨写丑陋的中国人已经20年了，那时我还买过丑陋的日本人和丑陋的美国人两本书。这三个国家中，中国人对于写自己丑陋的书反响之激烈让其他两国人诧异不已。结果呢？20年过去了，中国人的丑陋依然老而弥坚不减当年。柏杨只有“再为中国哭”一遍了。<br />
<br />
中国人的潜意识、潜规则太多了，以至于28层过滤以后，你还是不明白他倒底是什么意思---也许你最终会发现他嘴里说的意思和心里真实的意思正好相反。这潜意识、潜规则所带来的成本实在太高了，以至于我彻底打消了混入政坛、有所作为的企图，我清醒地认识到自己玩儿阴的玩不过那些人精，很可能被玩死都还不明白怎么死的。<br />
<br />
那些 &quot;反动网站&quot;(官方用语)，居然用因果报应阎王殿这些封建糟粕去吓唬人信他们，居然要靠丑化照片来蛊惑人心，居然要靠捏造事实来写政论文，这种下三滥的手段让人噤若寒蝉，就算我真同意他们有一点道理，也会耻于与他们为伍，真他妈的丢人现眼。泼妇骂街式的情绪，就算骂的全是道理，也会让人觉得那是泄私愤，甚至让人怀疑：他们被共产党揍是不是自己骂出来的现世报。<br />
<br />
“天安门母亲”，一群在6四时失去了孩子的亲属群体。其中有位母亲叫丁/子/霖，我尊敬她关于6四的反思，但我要指出一种可能性：让她们失去了孩子的这个责任，可能很大一部分要归于柴玲这个女人。当时各大高校的学运组织者大部分已经认识到势态的走向(如果复课还能将学潮带向体制改良的途径，如果继续对抗则可能导致决裂并葬送民运)，并在清场前已经集体开会通过复课决议。然而柴玲回到广场后又改变主意，否决了她自己同意的复课决议。在以后的活动中，她一而再再而三的改变自己在集体决策中表决过的主意，最后总是以将学生滞留在广场上为终极目标。 <br />
<br />
为什么柴玲非要将学生滞留在广场？根据她的录音，实际很简单：在她看来，如果大家复课，则当局会对学运领袖秋后算账。她就是要将事态闹大、就是要让广场血流成河震惊世界(据柴玲录音)才能保护她们这些学运领袖。所以在恐惧和侥幸心理之下，她一而再再而三的出尔反尔，竭力鼓动学生们滞留在广场上。而她自己早已打定主意要在血流成河前离开现场。<br />
<br />
现在的柴玲做什么去了呀？那个在广场上慷慨陈词、一开口先自我介绍“广场总指挥”的当年23岁的女孩子如今在做什么呀？她去了美国，成了哈佛MBA，开了一家惨淡经营的网络公司珍查巴尔（Jenzabar）。<br />
<br />
他们又在哪里去了呀？----王丹在哈佛大学拿到了历史学博士；刘刚在纽约高科技公司任职；周封锁在美国工作；梁擎敦在南加州从事金融股票工作；王超华在南加州工作；张伯笠在华盛顿传教；李录在华尔街从事金融业；熊炜在美国工作；熊焱参加了美国军队在伊拉克。吾尔开希在台湾成了家，作兼职电台节目主持人。翟伟民在河南一家公司工作；郑旭光在北京从事股票生意；马少方在深圳发展公司；杨涛在广东做生意......<br />
<br />
看到没有？以上大部分人如今都在务实地赚钱。我曾在一本扔在集装箱里从韩国运到中国的杂志上看到王丹在哈佛校园的照片，西装革履、英俊萧洒。这位哈佛大学的历史学博士这么些年来研究出些什么历史成果来了？屁，那些狗屁文章还不如猫眼一些普通网民写得多写得好。<br />
<br />
为什么？我不说别人，只说柴玲。这就是丑陋的中国人劣根性的吊诡：对很多中国人来说，不管一件事情在他嘴巴上说得如何冠冕堂皇，然而在内心里，他能否在那件事情上取得自己的利益，才是他是否值得做那件事的价值取向。当祖国好坏与否对远在美国的柴玲已经无足轻重、当她已经无法从渐行渐远的6四中获得任何影响和声誉(利益)时，当没有一个广场来作柴玲振臂一呼应者云集的爱国秀的舞台时，她也就自然将注意力转向了美元。<br />
<br />
[引用柴玲绝食书:]：然而，国家已经到了这样的时刻...国家是我们的国家，人民是我们的人民，政府是我们的政府。我们不喊谁喊？我们不干谁干？[引用结束] 我干他妈的。<br />
<br />
我已经够老了，等不了那么长时间了，怨毒让人身心疲。我不想象崔健那样把“一无所有”唱二十年还是一无所有，瞪着浮肿的眼袋看场内观众一年年稀薄。我不想再抄一遍“家祭无忘告乃翁”留给后人，而是想活着时就看到那一天。<br />
<br />
受错者和施错者，谁更难于走出和解的第一步？受错者需要暂时搁置一边的是痛苦和债权，施错者想要搁置一边却又无法完全自主的，却是羞耻和债务。如果施错者在和解之路上积重难返难以起步的话，受错者何不主动伸出手来呢？<br />
<br />
所以我想要和解了。和无常的宿命和解，和怨毒和解，和党和解。既然再大的怨毒都是要和解的，何必在乎谁先和解。我不再死拽着他从前的错喋喋不休，而是多说他如今做对的事，目的就是表达一个温暖的善意。等到大家真成了亲人的那一天，想必他会主动提起来以前的错。<br />
