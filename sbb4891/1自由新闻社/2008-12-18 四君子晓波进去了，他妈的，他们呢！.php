													<h2>也是一种说法</h2>
						
						
																											<div id="postmessage_11712" class="t_msgfont"><a href="http://www.pksina.com.cn/userindex/html/1/350098.htm" target="_blank">http://www.pksina.com.cn/userindex/html/1/350098.htm</a><br />
<br />
<br />
（转）吾尔开希、王丹、柴玲现在干什么？ - [政治]<br />
发布时间：2008-7-5 15:16:00 来源：沸点论坛<br />
天安门事件已经过去18年了，还有观众惦记着它。<br />
<br />
查看博客的访问统计，赫然发现，”64事件”、”吾尔开希”是最多的关键词。有一个关键词是”吾尔开希现在在干什么”，这引起了我的好奇，我也想知道，他现在在干什么。<br />
<br />
于是搜了一下。小吾同学先是去哈佛进修，然而不得清净，海外民主人士内斗不休，于是跑到台湾，结婚生子，成了电台主播和政治评论家。梅艳芳去世的时候，去香港送别，这是他与大陆距离最近的一次接触。关于他的新闻中，有许多色情新闻：招妓、64纪念日去夜店，诸如此类，莫辨真假。<br />
<br />
网上对他的评价，不外乎两个内容：1、吾尔开希是64学生领袖，激进的民运主义者。2、吾尔开希后来变成一个大腹便便的饶舌政论家。<br />
<br />
我在《论中国的学生暴动》一文中讲述了我对64事件的观点，事实上，由于环境的变迁和资讯的匮乏，我已经无法判断这次事件的真正意义何在，所以我给出了一个很黑色幽默的结论。<br />
然而尤使我悲哀的是，现在还有人用一种逻辑混乱的标准，去评价64以后的学生领袖们。最常见的是那种不假思索的愤青式逻辑：他们反对当权者，所以是自由英雄，民主斗士。当他们看到，这些英雄们也要吃喝拉撒，也干一些傻逼事情的时候，就开始陷入一种迷茫的愤怒，觉得自己被骗。一位”新闻工作者 黄伟国”的文章《管自己，不要管吾尔开希》写得很好：<br />
<br />
一些人先是将吾尔开希封作上帝，然后又痛心疾首，说发现了吾尔开希原来不是上帝。<br />
<br />
他还对这样的逻辑感到可笑：<br />
<br />
多年来，吾尔开希的腰围尺码像已成为中国民主前途指标，胖了就代表民运人士堕落腐化。<br />
<br />
在吾尔开希评论台湾政局的一段视频中（视频下载，我估计视频是台湾人做的），我看到他的确胖了，但语速仍如十八年前一样快。另外，作为一个拥护祖国统一的人，我对他动辄”国家”、”元首”的用词不感冒。在一个基于维基百科的网站中，把吾尔开希、王丹们列为”持不同政见者“，我觉得这是一个比较取巧但又尤为中肯的说法。<br />
<br />
至于王丹，他本来可以跑，但后来又甘愿跑回去坐牢。在1991年被北京法院以”反革命宣传煽动罪”判刑4年，1996年以”阴谋颠覆政returns the portion of府罪”判刑11年。 98年的时候”保外就医”，跑到美国去，也在哈佛念书。一些人口气强硬言辞凿凿说他是同性恋，仿佛同性恋是一个贬义词。我个人认为，王丹是64事件中头脑最清醒的人，他的低调也很合我的脾气。这里有一篇中国哈佛留学生的文章谈到了王丹：《近距离看王丹》。<br />
<br />
而柴玲，我没有兴趣去了解。我看到的那段64视频中，她简直就是中国愤青形象的代表人。用那一套官方话语，去反对官方话语。她的表现，显得缺乏智慧和尊严。据说她当了一家IT公司的CEO，正在大把捞钱，也算是回归社会主流.<br />
<br />
现在可以总结一下我的一些观点： <br />
1989年的理想与狂欢，斗争与流血，都跟1989这个年代一样过去了，它的意义无从说起。但仍有一些人不愿意忘记，愿意知道真相，我就是其中之一。 <br />
中国每个历史阶段都会出现一些刺头儿，他们是社会的解毒剂和信号，表示社会出现问题，但他们未必能提出解决问题的可行办法C遑论直接解决问题。 <br />
如果18年前的事件和人物，仍然关注的热点，18年前的问题，没有得到真正解决，这才是真正的悲哀。 <br />
评价一个人，要放到具体的历史语境中。对一个人做价值判断，是世界上最简单粗暴的事情，轻易为之，会显得自己浅薄可笑。 <br />
足够的资讯和独立思考能力，在这个时代是多么的珍贵。 <br />
这些观点都有些大而化之，甚至显得颇为虚无，但说实在的，为什么每件事情都要有一个盖棺定论呢？为什么每个问题，都要有一个统一答案呢？<br />
<br />
每一个问题，都应该至少有两种以上的解决办法。这个世界不是非此即彼，一个人也不是非好即坏。C如果说思考这些陈芝麻烂谷子非要总结出一些意义，那么这就是我得到的意义。说出这么老生常谈的句子，我自己也觉得有些不好意思。</div>

							
							
							
							
