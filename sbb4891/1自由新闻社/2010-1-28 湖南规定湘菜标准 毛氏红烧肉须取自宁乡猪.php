													<h2>湖南规定湘菜标准 毛氏红烧肉须取自宁乡猪</h2>
						
						
																											<div id="postmessage_456295" class="t_msgfont">核心提示：湖南餐饮业日前透露，湖南省湘菜地方系列标准将在5年内完成。《标准》对辣椒炒肉、毛氏红烧肉等菜肴进行了标准化描述，比如辣椒炒肉中辣椒和肉的比例为3：5、毛氏红烧肉所用五花肉要来自宁乡猪身上等。<br />
<br />
红网1月28日报道 切上肉，剁些辣椒，入锅翻炒，一道辣椒炒肉就完成了。以后您再做这道菜，就有具体的参照标准了。<br />
<br />
27日，湘菜地方标准审定会在华悦大酒店16楼会议室召开，此次会议审定了由省餐饮行业协会、省食品质量监督检测所共同起草的《湘菜烹调技术基本操作规范》，同时对湘式菜肴之四菜一汤(辣椒炒肉、毛氏红烧肉、酸菜蒸扣肉、剁(酱)椒蒸鱼头、龟羊汤)进行了标准化描述。例如辣椒炒肉中辣椒和肉的比例为3：5，剁椒鱼头之鱼头呈蝴蝶形状，毛氏红烧肉上所用五花肉要来自宁乡猪身上，酸菜蒸扣肉、龟羊汤分别有着辣度以及原材料地域来源上的要求。<br />
<br />
湘菜地方标准全系列5年内完成<br />
<br />
湖南省湘菜地方标准全系列将争取在5年内全部完成，《湘菜烹调技术基本操作规范》以及四菜一汤的地方标准有望在今年春节前发布。<br />
<br />
记者就标准是否会影响酒店湘菜同质化的问题采访了省餐协副秘书长高沉香。高沉香说：“湘菜标准不会影响酒店的自有特色，这只是一个推荐性标准，不强制其实施。制定这样一个标准出于多层面的考虑，比如可以统一湘菜理论教学教材，给喜欢湘菜的人提供参照物，促进湘菜原材料产业链的发展。”<br />
<br />
他说，目前湘菜的销售额在全国八大菜系中排名第三位。这个标准可以进一步提升湘菜文化，赋予湘菜个性，“因为越有个性的餐饮就越有市场份额。”<br />
<br />
各方对四菜一汤标准反响不一<br />
<br />
湖南秦皇食府餐饮集团董事长柏鹏认为，湘菜地方标准的颁布很有必要，这能让湘菜餐饮行业更加规范化、制度化。但对于酒店而言，基本标准不能是绝对标准，因为中国餐饮本身竞争就很激烈，如果都绝对地去遵循这个标准，就变成肯德基和麦当劳了。<br />
<br />
长沙辣椒炒肉土菜馆芙蓉路店的冯经理也说，他们这里的辣椒炒肉的肉和辣椒的比例大概为2：1，而且还要根据客人的口味。有的客人还希望肉少点，辣椒多点，湘菜地方标准中的辣椒炒肉的比例问题不能绝对化。长沙湘水人家的工作人员说，让剁椒鱼头呈蝴蝶装完全没有必要，只要好吃就行了。<br />
<br />
芙蓉国豪廷大酒店的工作人员小严却觉得这个标准很好，因为它可以将湘菜和别的菜系区分开来。<br />
<br />
在北京上班的湖南人小胡一听说有湘菜标准了特别兴奋：“家乡菜能有自己的特色和标准让全国人参考，很牛!”市民楚先生却说：“宁乡猪肉要比普通猪肉贵将近一倍呢，还难买。如果不是这种肉做出来的红烧肉，就不能叫毛氏五花肉了?”<br />
<br />
相关链接<br />
<br />
辣椒炒肉：肉和辣椒比例约5:3<br />
<br />
辣椒用红的青的都可以，但要肉厚皮薄的鲜辣椒，用肉需250克，其中瘦肉180克、五花肉70克，这个菜的菜肴特征应具备猪肉软嫩、青椒柔脆，咸香辣、酱香不腻等特征。<br />
<br />
剁椒鱼头要呈蝴蝶形状<br />
<br />
<br />
鳙鱼须来自无污染水质，可用背宰法宰杀，在盛装形态上鱼头呈蝴蝶状。红、白、绿相间有序，明快;装盘讲究，菜形、份量与盘碟协调具美感。在气味上、鱼肉要具有浓郁的剁辣椒风味，无腥味，无酱香味、无异味，吃上去能让人感觉鱼头酸辣微甜、坛香鱼香柔和，嫩滑爽口。<br />
<br />
毛氏红烧肉：选用宁乡猪的五花肉<br />
<br />
毛泽东最爱吃的湘味熟肉制品-----毛氏红烧肉要做得美味不容易，这道菜原材料为宁乡猪的五花肉，要经过预先熟制、红烧收汁等步骤才能完成，且在红烧收汁过程中要非常注意火候，标准上对这几个过程都有极其详细的说明。<br />
