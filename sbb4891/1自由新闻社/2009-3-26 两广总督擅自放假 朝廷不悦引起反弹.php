<br />
&nbsp; &nbsp; 新华社今日近12时发出快讯，国务院办公厅发出通知，要求严格执行国家法定节假日有关规定。通知说，法定节假日放假调休已有安排，各地要认真执行，不得擅自调休、自行安排。&gt;&gt;本网评论：广东人民奉旨违法的两难境地<br />
　　另据中广网消息，通知说，最近社会上出现了关于恢复“五一”长假的议论，个别地方也在考虑出台相关调整措施。国务院对此高度重视，责成有关部门进行了认真研究。经国务院同意，现就有关问题通知如下：<br />
<br />
　　一、严格执行《全国年节及纪念日放假办法》。这一办法是在广泛征求社会各方面意见和充分研究论证的基础上制定的，将部分传统节日增列为法定节假日，丰富了我国法定节假日的文化内涵，增加了广大人民群众的休假福利。新办法实施一年多年，总体运行情况良好，对于弘扬中华民族优秀传统文化、合理安排生产生活、满足人民群众休假休闲需求发挥了积极作用，受到广大人民群众肯定，各地区、各部门要严格执行。<br />
<br />
　　二、认真落实《国务院办公厅关于2009年部分节假日安排的通知》。该通知已对2009年元旦、春节、清明节、劳动节、端午节、中秋节和国庆节放假调休日期作出了具体安排，各地要认真执行，不得擅自调休、自行安排。落实《职工带薪年休假条例》，单位可根据生产、工作的具体情况和职工本人意愿，灵活安排。<br />
<br />
　　三、清明节和“五一”国际劳动节即将来临，各地区、各有关方面要及早合理安排节假日休闲旅游、交通运输、生产经营等活动，妥善安排好节日期间值班、安全、保卫等工作，确保人民群众平安祥和和度过节日假期。<br />
<br />
　　新闻延伸<br />
<br />
　　广东五一放假方案：1-7日放假<br />
<br />
　　25日上午9时，广东省政府召开“五一”黄金周新闻发布会，正式公布“五一”放假全套方案:5月1日至7日放假，其中1日为国家法定节日，2日、3日为周末，4日、5日与9日、10日对调，6日、7日为带薪年假。<br />
<br />
　　针对部分省份提出的变相恢复五一长假的措施，有专家提出质疑，这样的方式是否合乎程序？“广东省的做法只是用其他假日凑成了七天长假，并不存在违法的问题。问题在于，如果广东的做法被批准了，其他省份纷纷效仿又该怎么办？如果广东人在休假，而其他省份在上班，这样会不会影响工作呢？”<br />
<br />
　　国家信息中心高级经济师高清辉说，如果集中度假确实对社会和经济发展有利的话，广东的做法也没什么不对的，不涉及法律的问题，有关部门很可能会批准。不过，如果其他省份纷纷跟进，对于中央政策的严肃性将是一个挑战。<br />
