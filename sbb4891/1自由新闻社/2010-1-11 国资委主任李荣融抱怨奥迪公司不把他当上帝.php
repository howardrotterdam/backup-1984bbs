																											<div id="postmessage_425555" class="t_msgfont">李荣融不满奥迪车<br />
<br />
刘罡 <br />
<br />
国资委主任李荣融1月9日在第11届北大光华新年论坛上说，央企一定要做到以用户为上帝，他认为在这方面中国央企与丰田等日本一流企业还有很大差距。说这番话时李荣融突然有感而发道，德国企业在这方面做的也不怎么样。他以自己乘坐的奥迪车为例说，坐垫硬得要死，一点没考虑中国人没有那么大块头；既然明知是给部长级领导配的车，可车辆的仪表盘和所有控制按钮都在车前排，想听个收音机还要劳烦司机摁按钮。李荣融说，至少奥迪公司没把我当上帝。<br />
<br />
德国去年刚刚把世界最大出口国的宝座输给中国，新年伊始又受到李主任直言不讳的批评，可真够背的。李荣融的话虽然有很大调侃成分，但如果奥迪公司把中国消费者的意见一概视为耳旁风，很可能要在市场上栽跟头。因为经济实力日渐增长的中国，正在越来越多的领域重塑全球市场格局。<br />
<br />
以中国钢铁工业为例，预计中国今年的钢产量将达到创纪录的6亿吨，占全球产量的将近一半，是世界第二大产钢国日本的六倍。正因为中国在全球钢铁消费市场上占有如此举足轻重的地位，世界几大铁矿石供应商和中国钢铁业本应保持和谐的关系，但现实情况却正好相反，“力拓案”就是双方矛盾激化的一个反映。对几大铁矿石巨头来说，如果想当然地以为中国因铁矿石需求巨大而只能被动迁就他们是危险的。中国有句老话，水能载舟亦能覆舟，中国虽然未必能扭转铁矿石价格总体向上的市场趋势，但凭借自己占世界一半左右的铁矿石需求量，却有能力让市场价格产生人为波动。<br />
<br />
2009年全年中国铁矿石进口量达6.278亿吨，较2008年增长41.6%，而去年前11个月中国的粗钢产量只较上年增长了13%。这说明，因市场预计今年的铁矿石基准价格将上涨20%以上，交易员和钢铁生产商已开始预先储备铁矿石。设想一下，假如中国在抑制房价等方面的措施收到成效，将对国内的建筑用钢需求产生负面影响，如果届时铁矿石库存再出现过剩，很可能对全球铁矿石价格产生明显抑制作用，打铁矿石巨头一个措手不及。<br />
<br />
西方也有句老话──别把鸡蛋都放到一个篮子里。但对刚刚走出金融危机风暴的世界经济而言，许多行业除了把自己的鸡蛋放到新兴市场这只篮子里之外可能别无选择。人们普遍认为，以中、印、巴、俄这“金砖四国”为首的新兴市场国家将引领全球走出本轮经济衰退。但新兴市场股市比美国股市更为迅猛的反弹也暗示，投资者对新兴市场的复苏可能过于乐观了。如果新兴市场国家发生迪拜危机一类突发性事件，一些基于对新兴市场的乐观预期而加大马力生产的相关行业很可能遭受沉重打击。<br />
<br />
一句话，“上帝”是要尊重的，但天威莫测，“上帝”也是要提防的。<br />
