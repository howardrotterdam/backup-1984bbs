2010-07-28 08:11:42　来源: 中国网<br />
<br />
核心提示：中共中央政治局常委李长春参观吴冠中纪念特展时发表讲话，称赞吴冠中是德艺双馨的人民艺术家，并要求认真贯彻百花齐放、百家争鸣的文艺方针。<br />
中国网7月28日报道 中共中央政治局常委李长春同志2010年7月20日在参观“不负丹青――吴冠中纪念特展”时发表讲话：<br />
<br />
做德艺双馨、无愧于时代和人民的文艺家<br />
<br />
中国美术馆举办“不负丹青――吴冠中纪念特展”,很有意义。展览既展出了吴冠中同志的作品,以及手稿、画册、文集等珍贵资料;又展示了他高尚的人品风范、深邃的艺术思想、不懈的艺术追求、高远的艺术境界、卓越的艺术成就,让人们全面领略了吴冠中同志崇高的人格魅力和艺术魅力。<br />
<br />
吴冠中同志是中国当代美术界成就卓著、具有重要影响的艺术大师,是德艺双馨的人民艺术家。在长期的艺术生涯中,他始终关注现实生活、关注民族文化艺术的复兴,致力于中国艺术的探索与创新,主张博采中西、融通古今,创作了大量富有鲜明民族气派、时代特色、个性风格的油画和水墨作品,为中华民族文化艺术殿堂留下了丰厚的宝藏,为我国文化艺术和艺术教育事业的发展作出了不可磨灭的贡献;他淡泊名利、一身正气,体现出高尚的思想境界和人格精神,堪称文化艺术界学习的楷模。<br />
<br />
当前,我国经济发展,政治稳定,社会和谐,人民群众安居乐业,综合国力不断提升,国际地位显著提高,中国特色社会主义事业展现出勃勃生机,中华民族正昂首阔步走向伟大复兴。在这样一个历史性变革的伟大时代,文艺事业面临着难得的发展机遇,文化在推动经济社会发展中的作用日益凸显,人民群众的精神文化需求快速增长,科学技术日新月异,文艺创作环境更加宽松和谐。国运昌,文运兴。中华民族的伟大复兴必将伴随着民族文化艺术的伟大复兴。在这样一个文化建设的春天里,时代和人民呼唤更多无愧于历史、无愧于时代、无愧于人民的鸿篇巨制,期待涌现更多德艺双馨、深受人民欢迎和喜爱的名家大师。广大文化艺术工作者要学习吴冠中同志,做到德艺双馨,深受人民群众的欢迎和喜爱。<br />
<br />
学习吴冠中,就是要学习他始终不渝地热爱祖国、热爱人民,对祖国和人民无限忠诚的坚定信念。吴冠中早年留学法国学习油画,但他深信,“艺术的学习不在欧洲,不在巴黎……在祖国,在故乡,在家园”。学成后,他毅然放弃欧洲优越的创作条件,回到百废待兴的新中国,立志在祖国的土地上发展艺术事业。几十年来,他坚守艺术阵地,创作了一大批思想性、艺术性、观赏性俱佳的绘画作品,为我国当代美术事业的发展作出了杰出贡献;作为一名共产党员,他始终忠诚于党的艺术教育事业,为人师表、默默耕耘,诲人不倦、育人不辍,桃李满天下。广大文化艺术工作者要像吴冠中那样,忠诚于祖国和人民,自觉把个人的艺术追求融入国家发展的洪流之中,把个人的理想抱负同国家的发展和人民群众的需求紧密结合起来,在报效祖国、奉献社会、服务人民中实现自己的人生价值。<br />
<br />
学习吴冠中,就是要学习他坚持贴近实际、贴近生活、贴近群众,让艺术深深扎根于现实生活和人民群众沃土之中,把最好的艺术作品奉献给人民的高尚品格。吴冠中主张艺术家要关注现实、关注群众,注重在时代进步的伟大实践和人民群众的火热生活中汲取创作灵感,认为“绘画作品脱离了群众,犹如断了线的风筝”;主张艺术要为大众接受和欣赏,“画作要靠时间和人民来评判”。广大文化艺术工作者要像吴冠中那样,热情关注现实生活,积极投身亿万人民创造幸福生活和美好未来的伟大实践,自觉建立起文艺与人民群众之间的紧密联系,创作更多反映人民主体地位和现实生活的优秀作品,努力在人民群众的历史创造中进行艺术的创造,在时代的进步中推动艺术的进步。<br />
<br />
学习吴冠中,就是要学习他在艺术上砥砺磨炼、刻苦钻研、一丝不苟的执着追求。吴冠中曾说过,“爱上了美术……身家性命都属美术之家了。”几十年来,他全身心地投入到心爱的艺术创作中,致力于油画民族化和中国画现代化的探索,沉潜往复,孜孜以求,形成了鲜明的艺术特色,最终成为享誉海内外的一代绘画大师。广大文化艺术工作者要像吴冠中那样,树立高远的艺术追求,发扬十年磨一剑的精神,甘于寂寞、心无旁骛,潜心创作、精益求精,努力创作更多经得起历史和人民检验的精品力作、传世佳作。<br />
<br />
学习吴冠中,就是要学习他博采众长、勇于开拓、锐意进取的创新精神。吴冠中提倡艺术家要尊重传统,但不要盲目迷信传统;提倡要为我所用地吸收外国现代艺术长处,反对照搬洋人的艺术,主张中国人要挺直自己的脊梁,用自己的方式表达自己的思想感情。他融通中西、古今,把传统与现代、中华文化与西方文化、具象与抽象有机融合起来,为我国当代艺术的开拓创新和多元发展树立了典范。广大文化艺术工作者要像吴冠中那样,发扬创新精神,焕发创造激情,激发原创能力,大力弘扬中华民族优秀文化传统,积极学习借鉴世界各国人民创造的一切文明成果,博采众长、融会贯通、推陈出新,不断催生新的文艺样式和表现形式,为文艺事业繁荣发展注入旺盛的生机和活力。<br />
<br />
学习吴冠中,就是要学习他淡泊名利、无私奉献的崇高品格。吴冠中说过,“我吃的是草,挤的是奶。草,是长在祖国土地上的草,奶也应属于祖国和人民。”他生活俭朴、不尚奢华;他精心挑选出360余幅画作,无偿捐赠给中国美术馆、故宫博物院、上海美术馆、浙江美术馆、香港艺术馆等;他多次把作品拍卖所得款项全部捐赠出来;他生前留下遗愿,逝世后不举行遗体告别仪式,不举行追悼活动,以致于我要参加他的遗体告别仪式都无法实现。2006年春节前,我曾到吴老家中看望。他的居室和工作室都非常简朴,交谈中他的话语也非常朴实。当时,他谈到要陆续把自己的作品捐赠出来,奉献给人民,给我留下了很深的印象。广大文化艺术工作者要像吴冠中那样,把思想道德修养作为立身和创作之本,淡泊名利,甘于奉献,追求积极的人生价值,培养健康向上的审美情趣,始终牢记艺术工作的社会责任,始终保持高尚的精神境界和道德情操,努力用自己的人格魅力和艺术魅力赢得社会认可、赢得人民群众的尊重。<br />
<br />
总之,广大文化艺术工作者学习吴冠中同志,就是要学习他德艺双馨的崇高风范,争做德艺双馨的艺术大师。德艺双馨是对艺术家的艺术成就、艺术贡献和社会影响的最高评价,是每一位文化艺术工作者应该孜孜以求、毕生为之奋斗的至高荣誉。德,就是指个人品德、职业道德、价值取向、社会信誉,以及理想信念、思想境界、精神追求等,是中华民族优秀传统文化和社会主义先进文化精髓的集中体现,是文化艺术工作者立身处世之根。艺,就是指艺术才华、艺术能力、艺术思想、艺术风格、艺术境界等,是一个人艺术水平和艺术成就的集中展现,是文化艺术工作者成就事业之本。德与艺相辅相成、相互促进。人品决定艺品,立艺先要立德;同时,唯有德艺双馨,才能使高尚的人品和高超的艺品相得益彰、行之久远。德艺双馨不是行政任命的,是艺术家用实践铺就的人生轨迹,是历史和人民的客观评价。广大文化艺术工作者要自觉把德艺双馨作为一生为之奋斗的追求目标,积累丰富知识,提高精神境界,培养高尚人格,攀登人生和艺术高峰,做到修身与创作共进,人品与文品齐升,努力成为既具有高尚精神追求又具有高超艺术才华的艺术家,成为经受历史检验和人民推崇的名家大师。<br />
<br />
<br />
催生更多传世之作、涌现更多名家大师,需要有良好的艺术创作氛围和社会环境作保证。各级党委政府要从全局和战略的高度,充分认识繁荣发展文艺事业的重要性,把文艺工作放在重要位置,关心和支持文艺工作,不断提高领导文艺工作的能力和水平。要认真贯彻百花齐放、百家争鸣的方针,弘扬主旋律、提倡多样化,尊重文艺规律,尊重艺术家的创造性劳动,充分发扬艺术民主和学术民主,在艺术创作上提倡不同形式和风格的自由发展,在艺术理论上提倡不同观点和学派的充分讨论,在艺术发展上提倡不同品种和业态的积极创新,真正形成百花争妍、万紫千红的大好局面。要大力弘扬创新精神,培育创新文化,宽容挫折和失败,使广大文化艺术工作者的一切才华都有展示舞台、一切创造都有实现空间、一切贡献都能得到社会尊重,推动文化艺术工作者的创造精神和创造活力竞相迸发、充分涌流。要坚持尊重劳动、尊重知识、尊重人才、尊重创造,善于同文化艺术工作者广交朋友,政治上充分信任,创作上热情支持,生活上真诚关怀,为他们创作和生产创造良好条件。要积极开展正确的文艺评论,引导文艺思潮,尊重差异,包容多样,倡导不同艺术流派、不同学术观点之间相互尊重、相互切磋、取长补短、共同发展,促进文艺事业健康发展。要加强文化艺术工作者队伍建设,培养造就一大批文化艺术领域的领军人物和创新型、复合型、外向型、科技型人才,建立健全有利于优秀人才健康成长和脱颖而出的体制机制,努力形成一支老中青相结合的浩浩荡荡的文化艺术大军,为推动社会主义文化大发展大繁荣提供有力的人才保障。<br />
