今天终于可以松一口气了，我的朋友终于可以回家过年了。<br />
<br />
前段时间有个朋友从外地来北京开会，晚上10点多去洗脚屋洗脚，因经不住诱惑被小姐拽上床，正好这时警察赶到，被抓到治安支队，我们本来以为没有多大的事，罚款就行了，可谁知后来被治安处罚行政拘留15天，后来又听说拘留完后会直接转收容教育半年，这下我就比较急了，半年可怎么办，家庭和工作都没有了。只好托人来办这个事，因为我也是外地来京的，在北京也没有什么关系，只好托朋友来打听，最后听到一个朋友说需要花15万，还最少要在收教所待3个月。我觉得花的钱太多了，还有现在只不过是拘留15天，还不一定会收教半年，说不定到时候就放了。在朋友拘留期间，我托人介绍去拘留所看过3次，据说里面的待遇很不好（好，谁都愿意去了，起不到惩罚的作用），最后几天听拘留所的人说，朋友肯定会转半年收容教育的，让我赶快想办法，朋友也很急，告诉我不管花多少钱都要把他捞出来。<br />
<br />
我又开始想办法，最后一个朋友介绍在拘留所旁边有一个律师事务所，那里可以办，我给一个律师拿了3万，签了协议，办不了的话，退钱。15天过去后，朋友打电话告诉我，他要被送到收容教育所了，半年。律师告诉我现在是年关了，抓的比较紧，警察的任务也比较重，如果任务完不成的话，会直接影响他们的考评，所以现在没有人敢放，上面要求放的话，没有办法给办案民警交代。只好把3万快钱退给我（律师还是有一定的职业道德的，办不了就退钱）。<br />
<br />
这条路行不通只好等下一个环节来操作了,朋友拘留半个月后不出意外的被转到豆各庄收教所,Ы贪肽辍Ｖ苋接见日去送了一些日用品和书，在会见室看到很多人，朋友告诉我这些人都是因为同样的原因进来的，有老的又少的，各色人等都有。最后走的时候,朋友告诉我赶快把他捞出去,里面太乱了,忍受不了。我只能告诉他我会努力的。<br />
<br />
某天我在包头出差，朋友从拘留所里打来电话，告诉我他的管教可以帮他出来，需要５万，叫我赶快和他联系，周日回到北京，我给管教打电话，管教说见面谈，让我先带２万给他办事，剩下的３万以后再说。我们约在一个咖啡馆见面，他把具体的操作方法告诉了我，说是每个月北京收教所都会向河北邯郸或者是唐山收教所转送人员，下个月他可以安排我朋友转到河北，然后从那边出来。我想既然是收教所的管教，这事肯定没有问题了，就把２万元给了他。谁想过了半个多月，管教给我打电话，说现在是年关不是太好办，如果非要出来的话，需要１０万元，再给他３万，另外５万给河北收教所，如果同意的话，明天就可以把我朋友转到河北邯郸。我和朋友商量了一下，朋友说１０万也办。<br />
<br />
第二天我朋友被邯郸收教所派的车接到邯郸，同车共有１６０人。<br />
<br />
我想这个管教也太黑了本来说好的５万，现在一下子涨到１０万，太没有职业道德了。正好我有朋友在石家庄，我给他打了个电话，朋友一听说这事感到很惊讶，说石家庄嫖娼根本不是一个事，抓住了顶多交５０００元罚款，也没有听说还有从北京转到河北的，让我打听清楚了是不是转到河北。我说肯定是的，问他能不能办，他说没有问题，但是肯定是要花钱的。我说花钱不是问题（不想让北京那个管教黑我们）．<br />
<br />
当天下午我就开车到了石家庄，晚上他帮我介绍了一个朋友，说是省公安厅有关系，已经打过招呼了，人只要在河北肯定没有问题，公安厅的朋友已经和下面的公安局打过招呼了，周一上班就去接人．<br />
<br />
周一我和朋友一起开车到邯郸，接上市局的人一起到了邯郸收教所，见到了所长，所长无论如何也不放人，说我们手续不全，请假的话需要直系亲属申请（当地派出所要盖章），并且要医院的诊断证明，还有交３万的保证金，３千多元的生活费．没有办法只好再回石家庄去办资料，并且通知朋友的老婆来邯郸。<br />
<br />
在邯郸的时候听一个副所长说，这个所长已经宣布免职了，但因为没有新的人员到位，他还在主持工作，这是他最后一次捞钱的机会了，所以谁的面子也不给，他无所谓了。还听说所里的小卖部是所长的爱人开的，东西很贵假货居多，外面的东西不准往里送，只能从他们小卖部买。据说这些收容人员都是他们从北京买的，他们当地根本收不到学员，这种事派出所直接罚钱就处理了，所以他们为了创收，只好从北京购买人员，这次过来的１６０个学院其中有１６个是画勾的，意思是这１６个人是可以出钱的，所以想不花钱出去是不可能的。再说所长最后一次机会，能不疯狂吗？<br />
<br />
周三下午，我们在邯郸机场接了朋友的妻子，住了一夜．第二天早上去市局接上人来到收教所，这次有资料有人又有钱，倒是没有怎么为难，最后交钱走人．下午我们就回到了石家庄，朋友买了衣服洗了澡，吃过饭以后我把他们送到石家庄机场，我就直接回北京了．<br />
