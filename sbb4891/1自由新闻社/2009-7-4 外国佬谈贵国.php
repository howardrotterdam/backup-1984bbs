													<h2>非洲人眼里的中国人</h2>
						
						
																											<div id="postmessage_147019" class="t_msgfont">我这几年一直在非洲，通过密切接触部长、省长、企业家、酋长、村民等各个层次的非洲人，勾勒出非洲人眼里的中国人。我们可以以非洲人作为一面镜子，看看中国人在非洲人的心目中是什么样的人，以调整自己的不恰当行为，从而取得非洲人民的认同。正如唐代魏征讲：“以铜为镜，可以正衣冠；以史为镜，可以知兴替；以人为镜，可以知得失。” <br />
<br />
葡萄牙人于1418年达到西非海岸，从此，阿非利加不再孤独，这个欧洲殖民者竞技舞台的幕布徐徐拉开，上演着屠杀、掠夺与奴隶交易的悲剧。到1914年，除了埃塞俄比亚和利比里亚，整个非洲大陆被欧洲殖民者瓜分完毕，黑人不得不放弃自己从树上下来时就讲的语言，开始讲起了殖民宗主国的语言。二战后，非洲殖民地国家纷纷独立，欧洲统治者恋恋不舍地败走非洲，过去跟随欧洲人过来的印巴人填补了白人撤走后留下的经济空间。在经济全球化的今天，这个被称为地球上最后一座金矿的非洲大陆，已不再是黑非洲了，各种肤色的人在这里淘金，寻找财富，其中包括大约100万中国人。被白人统治一二百年的非洲黑人早已熟悉了白人的习性，但他们对中国人这个迟到的经济探险者还很陌生，他们看待我们，就像我们看待他们一样，从神秘、好奇开始，经过猜测、观察、实证，最后形成了一种观念。 <br />
<br />
中国人吃苦耐劳，这是非洲人对中国人一致的、积极的印象。中国人不管在非洲搞工程还是经商，他们几乎都没有周末和节假日，早7点上班，下午6点半下班，晚上还要加班。埃塞俄比亚NORTH WOLLO省WOLDIYA市的政府主席BALAY先生说对我说:“你们中国人工作很努力，这是你们国家很快富裕起来的主要原因，我们埃塞俄比亚人缺乏的就是这种精神。” 非洲人很佩服中国人的勤劳，有的甚至不可思议，认为中国人简直成了人肉机器。非洲虽然贫穷，可非洲黑人在劳动态度上与我们迥然不同。我们双周日实行还没多少年，可他们这里早就实行双周日了。加班，对中国人来说是家常便饭，甚至没有任何额外报酬。在这里，你让黑人晚下班十分钟他也向你要加班工资，不付就把你告上法庭。尽管加班工资比平常工资高出很多，黑人也不愿加班，他们要充分享受法律赋予他们的休闲。他们对中国人这种苦行僧式的工作态度实在无法理解，不知道中国人的快乐在哪里，不知道中国人在享受什么。他们经常和我讨论这样的话题：工作难道就是人活着的唯一目的么？如果人有追求快乐享受的权利，那只能认为这些闯非洲的中国人的快乐来自劳动，把劳动看作是一种享受。 <br />
<br />
<br />
<br />
中国人清心寡欲，这是非洲人对中国人的另一个印象。在非洲这个性比较放纵的地区，多数国家实行一夫多妻制，他们见中国人在这里单身一呆就是一年两年甚至三年，不找女人，实在无法理解。在非洲，经常见白人身边依偎着身材绝好的黑姑娘，也经常见健壮的黑小子挽着欧美来的白姑娘，孤独的就是中国人。我在埃塞俄比亚工作时，秘书Mesier曾问我，中国人出国前是不是都要打针。我问她打什么针，她说是打性的针。我以为是避孕针，便说不用打针，有安全套的。她说不是，是失去性的针，打了这种针后就没有性欲了，出国后就不想女人了。我听后很惊讶，她怎么会问我这个问题。我对她说，没听说过有这种针。她反问我说，那为什么中国人在国外不需要女人。我变法地解释道，我们中国人喜欢喝茶，喝茶泄火，常喝茶，就没性欲了，就不想女人了。你看，他们实在无法理解中国人的清心寡欲。 <br />
<br />
<br />
<br />
中国人制造的商品廉价，这是他们对中国人的第三个印象。非洲市场上中国商品最多的就是服装、小家电、五金和摩托车。一个中国同胞在那里为公司采购物资时，问当地的店主有没有便宜的，店主张口就来：“有呀，中国制造的就便宜。”在非洲，“中国制造”是廉价、劣质商品的代名词。一次，埃塞俄比亚交通部的Saife Berhane先生对我说：“我知道，你们中国有好产品的，可为什么出口非洲的中国产品质量这么差？我现在绝对不买中国制造的东西。” <br />
<br />
<br />
<br />
2009年1月31日，喀麦隆西北大区Bali的DOHGANYONGA III酋长同样问我这个问题：“喀麦隆市场上充斥着中国商品，可这些商品质量很差。”2009年5月18日，我在喀麦隆西北大区首府BAMENDA的DREAM LAND饭店吃饭，饭后和服务员Brenda聊天。她今年28岁，高中毕业后一直没有找到合适的工作。当她下班换上漂亮的裙子时，我问她这衣服是不是中国制造。她说：“你们中国的衣服是中看不中穿，看上去很漂亮，穿两天就坏了。我现在不买‘中国制造’了，我穿的裙子是尼日利亚制造的。” <br />
<br />
<br />
<br />
作为中国人，我当然要维护中国人的形象，我对他们解释说，我脚上穿的就是中国制造的李宁牌旅游鞋，从2006年穿到现在还没坏；我身上穿的也是在中国买的中国制造的衣服，质量蛮好的。中国人绝对能制造出高质量的产品，因为非洲市场的购买力有限，所以，我们就生产一些低成本的产品销往非洲，为了降低成本，质量当然就下降了，但我们也有高质量的产品销往欧美。解释归解释，在非洲，中国商品质量实在不敢恭维。我在喀麦隆西北大区住在临时租的房子里，厨房的水龙头坏了，便到省城买了一个，当然是“中国制造”，可没出两个星期就坏了，不得不又买了一个“中国制造”，因为在这里买不到别国制造的，换上后，还是用不住，就这样，在五个月里换了4个中国制造的水龙头。 <br />
<br />
<br />
<br />
2009年5月12日，我与喀麦隆政府官员SAMBA先生坐在车子里穿过喀麦隆的经济首都杜阿拉，我指着街道两边的商店自豪地对他说：“你看，商店里有80%的商品是中国制造。”他接着说：“怎么不见有中国制造的小轿车在非洲跑？”在非洲，许多中国企业不用国产汽车，支持民族企业，偏买小日本的丰田、三菱车，为什么？原因很简单，国产汽车质量不过关。一家在非洲的建筑企业买了两辆国产皮卡，用了不到一个月，就只能开动一辆了，另一辆停在院子里做配件用，几个月后两辆车就成了一堆废铁堆在那里，最后还是买了两辆日本丰田。 <br />
<br />
<br />
<br />
中国人法律意识淡漠，这是非洲人对中国人的第四个印象。可能是中国的封建社会太长了，人们习惯了家长式的管理，缺乏法律意识。在中国，企业不和员工签订合同，不上任何保险，随意解雇员工，这是家常便饭，已习以为常。在非洲，一些中国企业老板无视当地的法律，经常与当地雇员发生冲突，官司缠身，如雇工不签合同，不交纳各种保险，工资标准低于所在国规定的最低保障工资，节假日强迫员工上班，加班不付加班费，随意解雇劳工等。在中国，企业老板财大气粗，可以用钱摆平一切。在非洲，这就不灵了，老百姓都知道用法律维护自己的权益。在非洲的一家中国建筑企业，一天法院突然送来了150张传票。原来，这家企业没按照法律程序解雇员工，被解雇的员工纷纷到法院把这家企业告上了法庭。非洲虽然落后，但他们仍沿袭着殖民者带来的法律，现行的法律，几乎都是从英国、法国那里原封不动地搬来的。如在喀麦隆，西北大区和西南大区原来是英国殖民地，这里就采用英国的法律系统；其他八个大区原来是法国的殖民地，在那里就采用法国的法律系统。可能是殖民者多年来对他们教化的结果，非洲人办事一定要找出法律根据。中国人开农场办企业要租用土地，土地许可证上引用的法律法规就有20来项；你要购买政府管制商品，许可证上第一页就列满了法律依据。所以，中国人来非洲，第一件事就是要熟悉这里的法律，这样才能避免经营过程中因违犯法律造成的被动与损失。 <br />
<br />
<br />
<br />
中国人不注意形象，这是非洲人对中国人的第五个印象。非洲人被白人强迫穿上衣服后，他们竟保持了白人的传统，公务活动一定是西装革履。别看非洲原始，条件落后，可一些居住在城里的非洲人还是很讲究的，他们的卫生间里一定要有洗阴的器具，就是许多中国人把它当作小便池的东西。非洲原始，但并不是所有的非洲人都愚昧，他们也有现代文明意识，对一些恶习、陋习也是恨之入骨的。2006年9月21日，我从北京乘埃塞俄比亚航空公司的ET605航班去埃塞俄比亚首都亚的斯亚贝巴。飞行了6个小时左右，埃航的空姐找我，说有中国人在卫生间吸烟，让我用汉语广播，告诉中国乘客禁止在卫生间吸烟。广播完毕，我还是极力维护中华民族的整体形象，我对埃航的乘务员说，因为许多中国乘客不懂英语、法语，可飞机上的各种标记都是法语和英语书写的，中国人看不懂。在印度德里加油后起飞不久，又发现有人在卫生间吸烟。无奈，机乘人员不得不用汉语打印出警告，贴在每个卫生间的门上：对某些中国人在卫生间吸烟的行为已经广播警告，如再发现吸烟，将在飞机到达目的后交警察处置。2002年，一中国人从埃塞俄比亚乘坐埃塞俄比亚航空公司的飞机回国，他不听飞机上的警告，坚持抽烟，结果被罚款并在泰国曼谷机场扣留了一个星期。前几天，我在喀麦隆听一个从埃塞俄比亚来喀麦隆的中国同胞讲，和他同机的有几个中国同胞。开始，他还和临座的黑人聊天，黑人对中国人颇有好感。但飞行途中，一位同胞竟毫无顾忌地向地板上吐了口痰，然后用脚在上面碾压，让同机的老黑看了目瞪口呆，再也不愿搭理中国人了。在非洲，经常见中国人在车子里把鞋子脱掉，光脚凉在前挡风玻璃上，看上去很不雅观。在喀麦隆，我去过 TOTAL CAMEROUN、MTN CAMEROUN等外国人在喀麦隆投资的企业，从他们的办公环境到员工面貌，一看就是有实力的现代企业。进入办公楼大门，要用身份证换他们内部的电子证件，然后进每个门都用这个内部证件刷卡，来访的客人进入了哪个门，公司的电脑系统都有记录。在非洲的中国企业一般都是艰苦奋斗，办公场所简陋，尤其是一些建筑企业、百货商店，这些企业门面不注意装修，员工不注意形象，给非洲人留下不好的印象，好像我们中国人都很邋遢。一次，因工作关系我带埃塞俄比亚公路局的Abera Tilahun先生到一中国企业居住，晚上他在淋浴室洗澡，发现人们都裸体站在一排喷头下，之间没任何隔挡，他对我说：“你们中国人怎么都有露阴嗜？” <br />
<br />
<br />
<br />
中国人扰乱市场，这是非洲人对中国人的第六个印象。《中国非洲》(Chinafrique)一书的作者塞尔日・米歇尔认为，中国企业逐步把欧洲企业挤出非洲市场，这是中国人超低价格投标造成的。塞内加尔有个污水处理项目招标，中国人的报价还不到法国人的三分之一，气得法国人拽自己的头发。一些企业低价中标后，为少亏、不亏或赢利，便降低施工标准，违反合同规范，偷工减料现象严重，产品或工程质量无法保证。企业的生命力取决于产品的质量，产品的质量取决于人的质量。百年大计，质量第一，质量是企业的生命。一个企业如果没有危机意识，安于现状，忽视人和产品的质量，那企业必定是短命的。现在，非洲人也对中国企业操纵价格的行为颇为恐惧和愤怒，因为这抢了他们的饭碗，要么把他们挤出了市场，要么让他们没有机会进入市场。2007年我在埃塞俄比亚时，政府曾发布了一个通令，说大批中国人在埃塞俄比亚黑市用比尔兑换美元，造成埃塞俄比亚外汇紧缺，美元汇率上升。不但是外汇，就是木雕等旅游纪念品的价格，也被中国人炒了起来。好多非洲人说，你们中国人是祸水，走到哪里，就把哪里的市场搅乱。 <br />
<br />
<br />
<br />
中国人不团结，这是非洲人对中国人的第七个印象。在非洲，一个单位的中国人到外旅游遇到另一单位的中国人，他们很少打招呼寒暄，虽然远离祖国几万里，他们见面后如同见了外国人一样，没有任何亲切感。在非洲同一个城市开店的中国人，因为竞争关系大伤和气，有的发展到势不两立、不共戴天的地步。非洲某国的一个省会市有两家中国商店，其中的一家商店就放出风来，非要把另一家商店灭掉。在这里，帮派势力也很严重，来自中国各地区的人都形成自己的势力团伙，互相倾轧攻击，互相拆台。中国人做生意都有点猫腻，不是当地的黑人，往往是中国人之间互相向当局举报，可见手段之恶毒。 <br />
<br />
<br />
<br />
中国人没信仰，这是非洲人对中国人的第八个印象。在非洲，到政府部门办事或到法院参加诉讼，都要填写一系列的表格，其中有一项就是宗教信仰。这让我很为难，我填写什么宗教信仰呢？共产主义不是宗教信仰呀，开始我空着。他们问我，怎么不填写，我说无信仰。他们疑惑不解，人怎么会没有信仰呢。后来，非洲人总和我说起宗教信仰一事。问中国人去教堂么，都信仰什么宗教。2009年复活节，喀麦隆BUEA大学四年级会计专业学生Zita发手机短信问我相信耶稣复活么。中国宪法赋予公民宗教信仰自由的权利，可信教者在中国毕竟是少数，来非洲的中国人大多没宗教信仰的。非洲人见中国人星期天也不休息，从来不去教堂，对中国人的人生态度甚是不解。他们说，工作赚钱是人生的过程，但不是人生的目的。人生的目的是一种精神追求，人生的归宿是与上帝在一起。 <br />
<br />
<br />
<br />
中国人什么都吃，这是非洲人对中国人的第九个印象。中国人的食谱很广泛，几乎吃除了人之外的所有动物。和非洲人交流时间长了，他们总会问你：“听说你们中国人吃狗、吃猴子？”之类的话。在非洲某国，一家中国建筑公司偷吃了一只狗，当地黑工知道后集体罢工，表示抗议。还有一家建筑公司，刚来的时候附近水库里好多野鸭子，这家公司就把这个水库当作养鸭场，天天来抓鸭子吃，没几个月，鸭子就被吃光了，水面上漂满了野鸭毛。中国人在非洲，尤其是那些中方员工较多建筑单位，所到之处都搞“三光”政策，猪光、鸡光、野味光。一次，西北大区的工业局Beckley局长对我说：“当年你们中国人在这里修公路，把这里的猪都吃光了。你们中国人要来这里，须提前一年通知我们，我们好号召村民们为你们养猪。”来非洲的中国人，一定要尊重他们的风俗习惯，如在埃塞俄比亚，驴和狗是绝对不能吃的。 <br />
<br />
老子讲：“知人者智，自知者明；胜人者有力，自胜者强。”中华民族的优秀品质早已名扬天下，但我们仍有自身的缺点。你在人家门口做事，当然要考虑人家的感受。要想在非洲取得长远利益，每个到非洲的中国人都要自尊、自爱、自律、自强，给非洲人民留下良好印象，让非洲人民欢迎我们。要想让中国民族长久屹立于世界民族之林，就必须有危机意识，不要盲目乐观、夜郎自大，要辨证地剖析自己，既看到自己的长处，也看到自己的短处，要扬长补短，这样我们民族才能强大起来。如果连过去曾被人买卖的黑人都瞧不起我们，那我们这个具有五千年文明的中华民族真是无地自容了。（作者博客：飞翔的铁塔 <a href="http://www.aifeier.cn" target="_blank">www.aifeier.cn</a> ）</div>

							
							
							
							
