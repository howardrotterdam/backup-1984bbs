													<h2>贵州两台二战时美国发电机使用至今 一直正常发电</h2>
						
						
																											<div id="postmessage_779497" class="t_msgfont">近日，一篇“云南大山深处，惊现抗战时期两台美国发电机，从1942年到现在，一直正常发电”的帖子走红，昨日本报多方核实发现，帖子中所说的“史上最牛发电机”并非处于云南境内，而是身处贵州大山中。当地文物管理所表示，从开始发电至今68年，两台发电机运转正常。这也是此帖流传网络的原因之一，还引起了一场“国货”与“洋货”对比的热议。<br />
<br />
“我找到了两台传奇发电机”<br />
<br />
今年5月2日，网民“wutung”在汽车之家论坛发帖称，“从一个朋友那里得到消息，前不久，几个德国人来到中国，沿着滇缅公路一带寻找抗日战争时期美国帮助国民政府在抗战后方修建的一座地下水电站，想把其中两台美国发电机高价买走。”发帖者称，滇缅公路长达上千公里，为防日机轰炸，这两台美国发电机装在地下，地点极为隐秘，寻找的难度极大，德国人最后未能找到。发帖者还表示，他在贵州寻找一个月后，在当地村民的帮助下，“后来这两台发电机被我找到了。”<br />
<br />
与帖子一同发布了数张图片，图中配电柜电表显示这两台发电机还在发电，机身背面的铭牌上清晰地印有英文出厂地，其中混流式水轮机由美国勒菲尔公司制造，发电机由美国奇异（通用）公司制造。<br />
<br />
“wutung”的帖子引起了网民的兴趣，这个帖子不胫而走，近日流传网络。<br />
<br />
网民热议“国货”与“洋货”<br />
<br />
如今的桐梓县城供电充足，目前地下电站主要发电上网销售。虽然发电量不大，但是在网民看来，这里却有更重要的价值。<br />
<br />
地下电厂的发电机照片曝光后，网民认为，“看到这些照片，仿佛又回到了那个战火纷飞的年代，充满了传奇色彩！”网民“冬日飘雪”觉得，“天门河水电站见证了中国人民不屈不挠、坚持抗战的英勇历史，见证了中美两国人民在反法西斯战斗中结成的珍贵友谊”。<br />
<br />
网民话题的焦点集中在了“国货”与“洋货”的对比效果有了显例。<br />
<br />
“wutung”在寻访中其实先找到了一台1979年生产的国产发电机，已下岗报废了。“wutung”最后总结此行也表示，“找到这两台发电机使我颇有感触，这两台发电机，从1942年到现在一直在发电，连轴承都没有换过，这不能不说是个奇迹，如此质量，值得中国制造业借鉴，值得中国制造质量立法者借鉴。这恐怕就是德国人为什么要专程到中国寻找这两台发电机（的原因），足以说明它的历史价值和科学技术价值。”<br />
<br />
网民“少爷的雪铁龙”跟帖比较有代表性：“实际上国内有好多类似这样的东西，七八年前我和一个朋友（他搞矿山设备）去看一台旧的空压机组，转卖给福建的一位客户，我一看傻眼了，日本产的，铭牌上写着：昭和……年，忘了，算算也是解放前的东西！现在仍然在用！通过这两件事情，咱们不是看不起国内的产品，也不是崇洋媚外，咱们追求的是一个产品的可靠性和质量，如果国内生产企业也能生产出像样的东西来，谁还会花大价钱买进口货或者洋垃圾？”<br />
<br />
还有网民担心此帖一出，不是泄密了么？“wutung”的表示是，该地位于贵州大山深处，具体位置帖中并未透露。“wutung”回复网民为何要寻找这两台发电机的原因时还表示，“作为理工科出身，热衷于工艺和设计的我感到这两台发电机不一般。”<br />
<br />
两台美国发电机68年运转正常<br />
<br />
昨日，水电站所在的遵义市桐梓县水务局的一名张姓局长接受本报电话采访表示，当地确有一处天门河水电厂，里面有两台美国发电机，但具体情况自己并不了解。<br />
<br />
随后桐梓县文物管理所告诉本报，天门河水电厂于1939年开工建设，1942年正式发电，是贵州历史上的第一座设备先进的水利发电厂，为防日军轰炸，当年将重要设备均安装在人工凿成的地下岩溶洞中。<br />
<br />
抗战爆发后，国民政府军政部一家兵工厂迁至桐梓，为解决兵工厂电力问题，军政部在美国购发电机两台和配套设备，建天门河水力发电厂，由总工程师陈祖东设计，动用数千民工修建地下机房，经暗渠从小西湖引水发电。整个工程1939年动工，1942年两台发电机安装完毕开始发电，装机容量576千瓦。<br />
<br />
文物管理所还表示，从开始发电至今68年，两台发电机运转正常，这与网帖的说法一致。<br />
<br />
文物管理所工作人员说，自己小时候就听老人们说起这个地方，但现在的人很少知道。水电厂以前是军事禁区，水电厂筑坝发电的水库叫小西湖，风景优美，1942年张学良与赵四小姐在小西湖幽禁了两年多。<br />
<br />
据当地媒体报道和当地文化部门的记载，在地下机房门口除了有建厂碑，厂址内还有多处题刻，多为国民政府时期的高官所题写。其中有一处是中国国民党政治家陈立夫题写的“入天门而夺天工”，水电厂大坝碑座上刻有的《石工歌》，则记录了民夫们的艰苦生存状态。在洞外还刻着摩崖石刻。石刻上校徽图案表明，电厂是由当时内迁的东北大学、西北大学、清华大学、浙江大学、工业大学等国内5所著名高校的专家、教授齐心协力共同设计。<br />
<br />
地下电厂的两台水轮机组是在抗战期间从美国本土漂洋过海运至印度加尔各达，百余吨的零部件又经驻印美军“驼峰行动”的“巨型军用飞机”飞越喜马拉雅山运抵昆明，再经公路转运到遵义桐梓国民政府的这家兵工厂，安装于天门河发电，确保每天生产出200余支七九步枪、近100挺捷克式轻机枪。而当时在中印航线上负责押运任务的是蒋介石亲自委派的卡尔少校。<br />
<br />
美国生产厂家200万美元回购遭拒<br />
<br />
新华网贵州频道消息称，在网民“Wutung”透露德国人前来寻找发电机之前，早在1999年，美国机组的生产厂家勒菲尔公司得知自己上世纪40年代生产的机组还在使用，曾派人到桐梓查看，并提出用200万美元回购机组作为企业产品的形象和文化象征，但被文物部门婉拒了。<br />
<br />
实际上早在2006年，天门河水电厂已被评为贵州省第四批“省级重点文物保护单位”，当时曾拟向国家文物局推荐、申报第七批全国重点文物保护单位。<br />
<br />
文物管理所工作人员告诉本报，“近年来县里很重视文物保护，力度在不断加大，投入规模空前。”但据“Wutung”介绍，通往这里的路只有唯一一条山间小道，很难看到有文物保护单位的痕迹。</div>

							
							
							
							
