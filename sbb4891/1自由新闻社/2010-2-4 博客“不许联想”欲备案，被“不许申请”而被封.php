中国著名独立博客“不许联想”，因为没有ICP备案而被IDC(服务器托管商)2月2日晚开始停止对博客域名(wangxiaofeng.net)进行解析，导致博客无法访问。王小峰博客被封，在网上引发众多关注和讨论。昨日，博客域名一度解封，但随即博客再次被封，而且博客的紧急备用域名一起被封。<br />
<br />
　　“不许联想”的第三个域名到记者截稿(3日23时)时还能访问。<br />
<br />
　　“京ICP正在备”<br />
<br />
　　2月2日晚王小峰博客“不许联想”无法访问。资深媒体人王小峰的博客被称为中国互联网最有影响力的博客之一。<br />
<br />
　　在发现博客无法访问后，王小峰博客所在服务器的主人、帮王小峰维护博客的马日拉启用了备用域名以便能继续访问。<br />
<br />
　　马日拉说被封是因为没有备案，没有备案是因为备案一直没回音，“‘不许联想’一直托管在我北京的服务器上，两年多来，我们申请备案一直没有回音，所以只能在网页下方注明：京ICP正在备。终于熬到今天，被封锁了域名。”<br />
<br />
　　解封原来只是故障<br />
<br />
　　3日中午，也就是在被封十几个小时后，“不许联想”突然又恢复了正常访问，马日拉说，也许是因为王小峰博客的影响力以及网友的普遍关注，ID C不得不解除封锁，或者也许ID C只是在调试封锁系统而已。很多关注博客解封的网友也觉得可能是网络关注带来的变化。<br />
<br />
　　北大方正宽带网络科技有限公司工作人员在接受南都记者采访时表示其实并没有解封，“封是一定要封的，只不过是出了点故障导致意外可以访问”。<br />
<br />
　　这位工作人员称，按照工信部的最新通知，凡是没有通过IC P备案审核的网站，一律要加入黑名单，王小峰的博客因为没有IC P备案，所以ID C通过技术手段拒绝了其对服务器的访问。<br />
<br />
　　不久，博客果然再次被封，这次还捎带上了备用域名。第三个域名随即启用，截稿时还能访问。<br />
<br />
