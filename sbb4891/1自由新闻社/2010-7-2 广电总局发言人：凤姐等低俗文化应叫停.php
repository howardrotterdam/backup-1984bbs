<br />
<img src="http://img2.cache.netease.com/cnews/2010/7/1/20100701223439c4475.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
凤姐<br />
<br />
新华网7月1日报道 日前,国家广电总局办公厅主任、新闻发言人朱虹做客华中师范大学桂子山文人讲坛,为师生开展题为“媒介的责任”专题讲座,讲座中,朱虹表示,低俗电视节目应坚决叫停。<br />
<br />
<br />
朱虹说,电视节目性质有明确的区分:一种是正确引领先进文化的,如《感动中国》;一种是通俗化节目,如小沈阳的表演。而像“凤姐”这种低俗文化,应该坚决叫停,不让其流布于社会。朱虹坦陈当前电视节目的确存在一些问题,表现之一便是克隆现象严重。他以《超级女声》和刘谦的魔术为例指出,一旦某个节目火了,其他电视台就开始竞相进行复制、模仿,使观众产生审美疲劳。<br />
<br />
在谈起当前比较热门的婚恋节目时,朱虹表示,婚恋节目体现的是一种社会需求,这些节目出现的早期还是有其积极意义的,但后期便渐行渐偏,渐行渐远。这是需要警惕并加以防范的。电视台不能为了片面追求收视率,宣传金钱至上的思想或炒作个人隐私。<br />
<br />
谈及文化监管问题,朱虹指出,任何国家都有文化监管部门,只是管理方式不同;在文化监管方面,政府必须有所作为,放任不良文化因素蔓延,会造成不可收拾的局面。<br />
<br />
