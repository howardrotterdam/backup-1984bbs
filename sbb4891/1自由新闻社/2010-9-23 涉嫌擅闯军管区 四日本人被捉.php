<br />
中国过去曾多次在军事禁区拘捕怀疑间谍。 2002年 1月，济南军区拘捕了三名怀疑是间谍的人，三人被指以不同身份作掩护潜入部队训练场附近，以拍照、旅游为名刺探军事情报。 2004年 2月，南京军区则拘捕一批台湾间谍，当中包括前台湾军情局上校李运溥及其外甥。<br />
<br />
根据中国《军事设施保护法》，军事禁区等特殊地点和场合禁止外来人员拍摄、录像、录音、勘察、测量、描绘及记述，记者采访时也必须先经军方审查同意，方可进行。</div>

							
							
							
							
