石家庄有兵城之称<br />
<br />
【本报讯】中国人民解放军在沈阳、北京、济南、南京、广州、成都、兰州设有七大军区。北京军区作战任务是保卫首都安全，是次日本人闯入的河北石家庄是北京军区一大驻军重地，中国第一和第四王牌军的 38军和 27军均在该处部署，因而有「兵城」之称，属军事敏感重地。<br />
<br />
军事禁区多次拘怀疑间谍<br />
<br />
