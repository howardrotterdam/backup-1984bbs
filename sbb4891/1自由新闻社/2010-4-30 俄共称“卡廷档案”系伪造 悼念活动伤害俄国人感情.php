　<br />
　中新网4月28日电 据法新社报道，28日，应俄罗斯总统梅德韦杰夫的要求，俄国家档案馆公开了一份关于二战时期“卡廷森林事件”的秘密档案。<br />
<br />
　　据报道，俄罗斯国家档案馆28日发表声明称，“遵从俄罗斯总统梅德韦杰夫的命令，来自(总统密档)&quot;第一卷&quot;的&quot;卡廷事件&quot;真实档案文件的电子版已在国家档案馆网站上公开。”<br />
<br />
　　一名俄罗斯国家档案馆的发言人说，虽然这份档案已解密。但时至今日，这份档案仅向少数专门的研究人员公开，从来没有被放上网供普通人浏览。<br />
<br />
　　另据报道，上世纪90年代，俄罗斯总统密档第一卷被解密。这份密档是关于卡廷事件的专卷，其中共有三份文件。它开始由当时的苏联保管，除了苏联领导层中几位核心人物外，无人知道卷宗的内容。<br />
<br />
　　1990年4月13日，时任波兰总统的雅鲁泽尔斯基访问苏联时，前苏联总统戈尔巴乔夫向雅鲁泽尔斯基转交了部分有关卡廷事件的档案材料。不过，转交的材料中不包括总统密档第一卷。<br />
<br />
　　直到1992年10月14日，俄罗斯总统叶利钦的特使、国家档案馆馆长鲁道尔夫・皮霍亚前往波兰首都华沙，才完成了向波兰总统转交密档的使命。<br />
<br />
　　密档中的一份报告详细记述了“卡廷森林事件”的来龙去脉。<br />
<br />
　　1940年左右，约2.2万名波兰军人和知识界精英在苏联斯摩棱斯克州以西的卡廷森林被集体杀害。1943年4月13日，攻入苏联的纳粹德国宣布在卡廷森林发现大批波兰军人尸体，并称杀害事件是苏联所为，但苏联随即予以否认，并反驳说此事是纳粹所为。<br />
<br />
　　直到1990年4月，苏联才承认为这起事件负责。<br />
<br />
　　今年4月10日，波兰总统卡钦斯基夫妇乘坐专机，前往俄罗斯出席“卡廷森林事件”70周年的纪念活动。专机在俄罗斯斯摩棱斯克一间机场附近坠毁，包括波兰总统及其夫人、军队参谋长、副外长等高官在内的机上96人全部遇难。</div>

							
							
							
							
