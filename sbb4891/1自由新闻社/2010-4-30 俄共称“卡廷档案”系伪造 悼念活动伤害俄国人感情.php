													<h2>俄罗斯总统下令公布卡廷屠杀事件绝密文件</h2>
						
						
																											<div id="postmessage_591572" class="t_msgfont">2010-04-29<br />
俄罗斯总统28日下令公布卡廷事件绝密文件，文件证实斯大林亲自下令批准卡廷大屠杀，俄罗斯共产党仍坚持苏共就此问题论调。自由亚洲电台特约记者周人报道。<br />
<a href="http://www.rfa.org/mandarin/yataibaodao/e-04292010123340.html" target="_blank">http://www.rfa.org/mandarin/yataibaodao/e-04292010123340.html</a><br />
<br />
<br />
<br />
从本星期三开始，俄罗斯民众已经能够在俄国家档案馆网站上自由浏览下载关于卡廷大屠杀惨案的历史文件扫描档案，而这些曾先后被放置在苏共中央总务部、苏共中央政治局和克里姆林宫总统档案馆的文件在前苏联时期和此前的俄罗斯被视为最绝密的档案。在苏联解体前除了苏共领导层的几名核心人物外，没有任何人知道文件的内容。<br />
 <br />
梅德韦杰夫28日在访问瑞典时对此表示说：“根据我的命令，今天这些文件已经可以在俄罗斯国家档案馆网站上被自由阅读，人们可以看到，是谁下令枪杀了波兰军官们，文件上写得明明白白，上面都有签字，公布历史真相的文件，这是我们的责任。”<br />
 <br />
据俄国家档案馆网站公布的关于‘卡廷 问题’历史文件‘专卷密档第一卷’的电子版中一份文件显示，苏联秘密警察头子贝利亚在1940年3月5日签署了一封“绝密”信件，建议处决波兰战俘，在这份文件上签名的除了斯大林，还有莫洛托夫、米高扬等三名苏联政治局成员，签字批准采取‘最严厉的惩罚手段――枪决’。这份死刑执行命令称这些被枪杀的波兰人为“反革命分子”和“民族主义者”。<br />
 <br />
卡廷事件又称卡廷惨案、卡廷森林大屠杀，是指第二次世界大战期间的1940年春天，大约2万2千名波兰军人、知识分子、政界人士和公职人员在俄罗斯斯摩棱斯克附近的卡廷森林被苏军集体屠杀。1943年4月， 攻入苏联的纳粹德国宣布在卡廷森林发现了被苏联军队杀害的大批波兰军人尸体，而苏联对此予以断然否认并称是德军所为。此后，苏联和德国均相互指责并各自组织调查团前往卡廷进行实地调查均无结果，从而使之成为一桩世纪迷案，直到1990年苏联末代总书记戈尔巴乔夫才承认前苏联对屠杀负有责任。卡廷惨案使波兰整整一代人的精英被苏共和斯大林屠杀，成为20世纪波兰人在苏联统治时期不断遭受痛苦的象征。<br />
 <br />
俄罗斯总理普京在4月7日于卡廷森林参加大屠杀遇难者悼念活动时，也强烈批评斯大林的迫害，他表示说：“斯大林的独裁统治的罪行无论如何也不能够被以任何形式开脱，无论历史真相如何令人痛苦，我们也应该保存并将历史真相大白于天下”。普京还表示，不应该将今天的俄罗斯人同当年的斯大林迫害相联系。<br />
 <br />
就在各界对莫斯科公布卡廷大屠杀机密文件给予积极评价的同时，俄罗斯共产党则声明说：这些文件纯属伪造，大屠杀事件是纳粹所为，这是波兰人在利用卡廷悼念仪式进行反对俄罗斯的行为，而且还得到了普京的支持，这些行为侮辱了俄罗斯人民的感情。<br />
 <br />
