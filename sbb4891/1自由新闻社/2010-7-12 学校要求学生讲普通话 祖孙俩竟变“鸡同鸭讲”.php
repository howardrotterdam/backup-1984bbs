																											<div id="postmessage_734697" class="t_msgfont">学校要求学生讲普通话 祖孙俩竟变&quot;鸡同鸭讲&quot;<br />
<a href="http://news.ycwb.com/2010-07/09/content_2571999.htm" target="_blank">http://news.ycwb.com/2010-07/09/content_2571999.htm</a><br />
重点关注<br />
<br />
越秀区执信南路小学“要求”学生不管是上课还是课余时间，都要讲普通话<br />
<br />
因为在执信南路小学上学的女儿粤语说得不好，广州市民曾先生家最近爆发了一场家庭纷争。原来，执信南路小学“要求”学生不管是上课还是课余时间，都要讲普通话，时间一长，曾先生的女儿粤语荒废，以致在家也讲普通话，这令得不怎么听得懂普通话的祖母大发脾气。<br />
<br />
孙女不讲粤语祖母发脾气<br />
<br />
曾先生是个地道的广州人，他的女儿小雯（化名）在执信南路小学读四年级，普通话说得很流利，但是粤语连个菜名都需要用普通话翻译过来。小雯的祖母年纪大了，不怎么听得懂普通话，跟孙女沟通起来很不方便，平时就有些抱怨。<br />
<br />
几天前，放暑假在家的小雯想吃炸鸡翅，就叫祖母去菜场买，但祖母听不懂普通话发音的“鸡翅”，最后没买回来，祖孙俩为此闹起了别扭。一家人晚餐时，祖母气不过冲着儿子儿媳大发脾气，指责他们没教好孩子粤语，称“这叫忘了根本”！<br />
<br />
曾先生后来了解到，今年执信南路小学极力推行普通话教育，有一条“不成文”的规矩，不仅要求学生上课要讲普通话，还要求学生在课余时间与同学们聊天、玩乐时也要讲普通话，“他们的口号是，普通话是校园的语言”！<br />
<br />
“学校专门让环保小卫士来监督，一旦发现有学生讲粤语，就会登记名字，交给老师处理。听我女儿说，违反规定就要扣学分，被扣10分以上就不准做班干部，还要全校点名批评。”曾先生告诉记者，女儿因为害怕被扣分，再也不敢讲粤语，久而久之就不会说了。<br />
<br />
部分家长认为禁粤语欠妥<br />
<br />
“新广州人”林先生家也遇到了相同的问题，他的孩子在执信南小学读六年级。对于学校禁止学生讲粤语的做法，林先生说，学校的初衷是为了推广普通话，但不应该要求孩子在课余时间也必须讲普通话。<br />
<br />
“我们以前来广州找工作时，因为不会粤语而错过了不少工作机会。如今虽然成为广州人，但语言问题仍然不时影响到工作。我们不希望自己的过去在孩子身上重演，所以希望孩子能及早说一口流利的粤语。” 林先生说。<br />
<br />
在执信南路上班的李姨是个老广州，她表示，粤语是本地方言，作为一个广州本地人，讲不好粤语说不过去，但普通话也要学好。采访中，家长大多有类似的想法，他们都觉得学校严厉“要求”孩子们在课余不能讲粤语的做法欠妥。他们希望学校既提倡孩子们在课堂讲普通话，但不能禁止孩子们在课余讲粤语，毕竟双语教学最适合孩子。<br />
<br />
老师称学校做法无可厚非<br />
<br />
昨天下午，记者想就“封杀”粤语一事采访执信南路小学负责人，但在校门口被保安拦住。后来，保安叫来一位男性老师，记者提出想进学校看“禁止讲粤语”的相关规定，但对方表示“没有文字规定”，并表示，“要想进校内采访，需经区教育局批准”。<br />
<br />
该校一位不愿透露姓名的老师事后告诉记者，“要掌握一门语言，光靠课堂上的一点时间，肯定是不够的，必须在日常交流中大量使用，学校要求学生在校内多讲普通话的做法无可厚非”。这位老师希望家长“要理解学校做法，绝对是为了孩子好”！<br />
<br />
记者随后采访了越秀区教育局。该局有关人士只给出一句话答复：教育部门没有明文规定学生在校园一定不许讲粤语，只是提倡大力推广普通话。<br />
<br />
学校“封杀”粤语，你有何看法？请致电本报热线87776887<br />
<br />
羊城晚报记者 胡军 实习生 资勇庭<br />
<br />
最近在做一个关于广州北岸文化广场的一个项目（同时包括了红砖厂和发电厂等地）<br />
设计文件里面甚至是重点提出了对于岭南文化的复兴等等<br />
但是现在最近突然又来了个废除粤语的口号 突然觉得好笑 难道粤语就不是岭南文化的一个重要组成部分么？<br />
语言的作用是辨别是否同族与异族 以及是保障本身文化不受干扰的一个重要工具<br />
当只有一种语言的时候 那么本地文化也会被大同话 <br />
当一边高喊着保护南粤文化 or 岭南文化 一边是废除粤语的时候 不觉得搞笑么？<br />
或许 最后剩下的只是一些如同油脂粉面的装饰的东西 真正的精髓 或许已经如同粪土般的抹去<br />
国语好粤语好都有存在的理由 也有他们的作用 想说什么话是自己决定的 不是某个人拍下脑袋决定的<br />
