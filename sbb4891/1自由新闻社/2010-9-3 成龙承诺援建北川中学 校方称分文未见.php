																											<div id="postmessage_810236" class="t_msgfont">成龙承诺援建北川中学 校方:分文未见<br />
<br />
2010-09-02 22:07:33　来源: 大江网(南昌)　<br />
<br />
核心提示：成龙于2009年承诺将电影《大兵小将》部分票房捐出援建北川中学，该电影票房达1.53亿。负责北川中学援建的中国华侨经济文化基金会及北川中学校长刘亚春证实成龙曾作出承诺，但均表示并未收到承诺款项。<br />
<br />
<br />
<br />
<br />
<br />
<br />
2010年2月14日《大兵小将》首映。一个月后，《大兵小将》在内地以1.53亿元人民币票房收官。<br />
<br />
<br />
<br />
日前，本报记者致电北川中学校长刘亚春，询问成龙是否在那天的活动上承诺过捐赠。刘亚春沉吟了一会，清楚地回答：“是。”他强调：“但我们从未收到过这笔钱。”<br />
<br />
大江网9月2日报道 2010年9月1日，由海内外华侨华人援建的新北川中学开学，意外引发香港艺人成龙捐款悬疑……<br />
<br />
2009年5月11日，“香港艺人成龙和李宇春、刘媛媛、金培达等探访北川中学，成龙更承诺捐献新作《大兵小将》部分票房援建北川中学。”(见香港《文汇报》2009年5月12日报道)<br />
<br />
负责新北川中学援建的中国华侨经济文化基金会告知《上海侨报》：“我们没有收到他所说的这笔钱。而且我们曾致信成龙先生，询问他所承诺的这笔善款，但一直没有回音。”<br />
<br />
记者联系负责新北川中学援建捐赠事宜的中国华侨经济文化基金会。面对记者询问“2009年5月11日的纪念活动上，成龙是否做出过要将《大兵小将》部分片酬捐给北川中学用于重建的表示？”对方沉吟片刻，然后明确表示“是”。<br />
<br />
记者随后在中国华侨经济文化基金会的网站上，看到了目前的北川中学捐赠人名单，既有捐款额高达数百万元的捐赠者，也有捐出20元人民币的捐赠者，但并未发现成龙、陈港生(成龙原名)或北京成龙慈善基金会的名字。<br />
<br />
称呼成龙为“大哥”的不只是学生，记者从网络上保存的视频资料中看到，当天舞台上，北川中学校长刘亚春客气地称成龙为“成龙大哥”。记者致电刘亚春，询问成龙是否在当天的活动上说过承诺捐赠的话。刘亚春沉吟了一会，清楚地回答：“是。”他强调：“但我们从未收到过这笔钱。”<br />
<br />
会不会是匿名捐款？记者致电北京成龙慈善基金会秘书长党群，她想了一下，告诉记者：“我们好像没有针对北川中学捐过款。”<br />
<br />
<br />
成龙影业集团中国部总经理袁农在成龙“挺菲”言论爆出后，其手机就一直“无法接通”，不久之后变成了“您拨打的号码已停机”。按照《大兵小将》官网的说法，袁农是该片的监制，并负责成龙的慈善事务。 （上海侨报供网易专稿）<br />
