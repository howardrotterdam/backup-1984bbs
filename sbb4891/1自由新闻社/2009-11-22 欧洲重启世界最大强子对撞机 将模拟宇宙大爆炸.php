																											<div id="postmessage_337764" class="t_msgfont">欧洲重启世界最大强子对撞机 可模拟宇宙大爆炸<br />
<br />
2009年11月22日09:34<br />
<br />
<img src="http://img.hexun.com/2009-11-22/121771362.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　大型强子对撞机位于瑞士、法国边境地下100米深的环形隧道中，隧道全长达27公里资料图片<br />
<br />
<img src="http://img.hexun.com/2009-11-22/121771363.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
ATLAS是一台巨型数字照相机，能够拍摄质子间6亿次碰撞的照片资料图片<br />
<br />
　　欧洲大型强子对撞机重新启动<br />
<br />
　　大型强子对撞机因氦泄露“休眠”14个月后再次重启。欧洲核子研究中心20日说，第一束质子束当晚贯穿整个对撞机，标志着对撞机恢复正常运转，为明年获取对撞机初步物理实验结果迈出重要一步。<br />
<br />
　　提前9小时<br />
<br />
　　欧洲核子研究中心发言人詹姆斯吉利斯说，当地时间20日22时（北京时间21日6时）左右，第一束质子束沿顺时针方向贯穿对撞机，比预计时间提前大约9小时。<br />
　　吉利斯告诉美联社：“（当时）部分科学家已回家，让我们叫了回来。”<br />
　　大约两小时后，科学家将第二束质子束注入对撞机逆时针运行。<br />
<br />
　　“看见质子束流再次在大型强子对撞机内运行很不错，”欧洲核子研究中心主任罗尔夫霍伊尔说，“我们距离开始物理（实验）仍有一段路，但这是个里程碑，我们进展顺利。”<br />
<br />
　　欧洲大型强子对撞机是世界最大的粒子加速器，建于瑞士和法国交界地区地下100米深处、总长大约27公里的环形隧道内，用于研究宇宙起源和各种基本粒子特性。来自80多个国家和地区的大约7000名科研人员参与对撞机建设。<br />
<br />
　　有望超费米<br />
<br />
　　当晚质子束运动速度较慢。吉利斯说，如果不出差错，欧洲核子研究中心有望提高束流速度，争取周末实现单束能量达到1．2万亿电子伏特。<br />
　　当前世界最强大的加速器是费米国家实验室Tevatron粒子加速器，位于美国芝加哥，单束能量可达1万亿电子伏特。<br />
　　开足马力的对撞机能够确保数万亿粒子高速流过将近27公里长的地下隧道，最高速度接近每秒30万公里，相当于光速的99．99％。届时质子束每秒可在隧道内狂飙11245圈，单束能量达到7万亿电子伏特。<br />
<br />
　　“下一个里程碑将是低速碰撞，有望一周后实施，”吉利斯说。<br />
　　按照先前计划，科研人员将以较低速度、较小规模实施粒子对撞，积累经验后启动粒子高速对撞实验。届时，相向运行的两束高速质子束将在隧道内对撞，碰撞点将释放巨大热量和能量，模拟出宇宙大爆炸发生时情况。<br />
　　当前盛行的宇宙学观点认为，宇宙起源于大约150亿年前一次大爆炸。<br />
<br />
　　汲取前车鉴<br />
<br />
　　大型强子对撞机2008年因氦泄露停止运转后，欧洲核子研究中心历时1年多、花费4000万美元修理并加固，避免重蹈覆辙。<br />
　　对撞机2008年9月10日正式启动，当月19日在隧道第三段至第四段尝试5万亿电子伏特质子束运行时，因两块磁铁间电连接部件在强电流通过时融化引发氦泄漏，对撞机被迫停止运作。<br />
　　英国广播公司报道，科研人员更换53块超导磁铁，修复大约200个电连接部件，安装新预警系统，以防再发类似事故。<br />
　　“我们对这台大型强子对撞机的认识比一年前深入得多，”欧洲核子研究中心加速器负责人史蒂夫迈尔斯说，“我们从经验中吸取了教训。”<br />
