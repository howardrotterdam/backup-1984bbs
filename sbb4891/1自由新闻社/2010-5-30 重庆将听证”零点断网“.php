　　<img src="http://news.xinhuanet.com/politics/2010-05/29/12157454_11n.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　<font color="DarkRed">2月3日，安徽省合肥市绿色上网辅导员史坤指导孩子文明上网。新华社记者 刘军喜摄 资料图片</font><br />
<br />
　　<strong>重庆拟立地方性法规保护未成年人</strong><br />
<br />
　　<strong>将就零点断网召开听证会</strong><br />
<br />
　　关注农村留守儿童、零点断网……近日，在重庆市人大常委会第17次会议上，这些保护未成年人的措施成为审议《重庆市未成年人保护条例(草案)》的热点焦点。这些措施是否可行、能否写进《重庆市未成年人保护条例》，常委会组成人员在分组审议中进行了激烈讨论。<br />
<br />
　　据重庆市人大内务司法委员会主任委员杨颖嘉介绍，2010年年初，重庆市人大内务司法委员会与共青团重庆市委组建条例起草小组开展论证。条例共有8章，分别在家庭、学校、社会等多方面作出具体规定。<br />
<br />
　　杨颖嘉称，重庆是农村劳务输出的重点地区，农村外出务工人员近700万，有200多万未成年子女，占农村地区未成年人总数的半数以上。此项草案分别从政府职责、家长义务、社会关怀等不同角度对加强留守儿童监护作出具体规定。<br />
<br />
　　草案规定，父母或其他监护人因外出务工或其他原因不能履行监护职责时，应委托有监护能力的其他成年人代为监护，并将委托监护情况告知未成年人子女所在学校和户籍所在地或居住地村民委员会、居民委员会；外出务工人员较集中地区每个乡镇至少建一所寄宿制学校，在乡镇、村社推行外出务工人员子女托管服务机构建设，并对家庭经济困难的未成年学生给予寄宿费用减免或资助。<br />
<br />
　　“零点断网”，即在法定营业时间以外切断网吧的网络接入。据介绍，此前，重庆市人大内司委就“零点断网”问题专门制作了600张调查问卷，广泛征求了区县人大常委会、人大代表、教师、学生、专家学者等意见。<br />
<br />
　　问卷统计显示：85.5%的被调查者认为“零点断网”可以实现，都认为与上位法不抵触，有利于在较大范围内阻止未成年人零时后进入网吧，制止网吧违法经营，而且从技术上也完全可以解决。<br />
<br />
　在分组审议会议上，大多数委员认为应该施行“零点断网”措施。首先，国务院《互联网上网服务营业场所管理条例》第二十二条规定，“互联网上网服务营业场所每日营业时间限于8时至24时”；其次，2008年8月，全国人大常委会执法检查组《关于检查〈未成年人保护法〉实施情况的报告》指出，应严格实行上网实名制和“零点断网”，对接纳未成年人和超时间经营的网吧给予重罚，同时依法追究经营者责任。“零点断网”的反对者则认为，其与上位法抵触，没有法律依据，而且从技术上很难实现。<br />
<br />
　　尽管多数人表示支持“零点断网”，但是，重庆市人大常委会依然很慎重，目前暂未在草案中写入“零点断网”的内容。杨颖嘉解释说，因为全国目前尚没有地方性法规作出“零点断网”的规定，下一步他们将专门就“零点断网”问题召开听证会，如果时机合适，再考虑加入“零点断网”的相关内容。<br />
