													<h2>大学生在博客微辞世博会被“喝茶”</h2>
						
						
																											<div id="postmessage_613078" class="t_msgfont">Twitter上看到的，转过来<br />
<br />
“最后一课”：第一次被“喝茶”<br />
(2010-05-12 05:40:25)<br />
作者：最佳损友<br />
博客原地址：<a href="http://blog.sina.com.cn/twitter2020" target="_blank">http://blog.sina.com.cn/twitter2020</a><br />
<br />
从昨天（5月10日）开始，我现在的辅导员就不断给我打电话，由于我在街上，好几个电话都没接，后来接通了，她说我有点东西没办理好，这关系到毕业证的问题，问我是否可以赶回来。后来我就大老远的乘车回学校，辅导员又给我打了几个电话，最后告知我的是今天（5月11日）上午到她的办公室来。我先前觉得她好像从来都没有这样的“关心”过我，这个时候突如其来的“热情”让我有点不自在，这样我也对她很客气，但我并没有任何其他方面的疑虑。<br />
<br />
（5月11日）上午，我按照预定的时间赶往了她的办公地点。当我敲开门，我没有看到辅导员的身影，通过所穿的制服可以得知是国\保们正在那里等我，头脑里顿时有一种不祥的征兆。尽管我预知到这一天迟早都会到来，但还是觉得来得是不是太早了一些，我分明感觉到一种苦涩和良心不安相交织所产生的复杂的感情。<br />
<br />
我有一点紧张，我担心是不是会被带上警车，有一种天塌下来的感觉。他们似乎看到了写在我脸上的紧张表情，安慰我叫我不要紧张，对我很“热情”，校党委书记不断的倒茶给我喝。在这个利欲熏心和冷漠的国度里，我一直遭遇的都是来自于权势阶层和自以为是者的白眼和颐指气使，当我受到来自于权阶所给予的这样的特殊“礼遇”时多少有点受宠若惊的感觉，我和他们的人格和尊严似乎暂时“平等”了。“不要紧张，我们今天来主要是想和你交流一下。”我们的“交流”就这样开始了。而这极有可能是我在这个学校甚至我的学业阶段的“最后一课”。<br />
<br />
国\保：你在哪里开的博客？是不是写过世博会的文章，什么文章?<br />
<br />
我：新\浪。写了世博的十宗罪。可能是因为我不是名人，我不是韩寒，所以你们才会来找我。确实我也受了韩寒较大的影响，他的很多小说我都看过（国宝插话“三重门”）。是的，还有其他小说和博客，我也一直很喜欢他，今天对我来说很不幸。<br />
<br />
<br />
国\保：韩寒是韩寒。你对世博会有什么看法？<br />
<br />
我：我不反对世博会本身，但是反对世博会被政治化。世博会作为一个文化和科技方面的展览会在中国举办，可以使参观者受到启蒙，扩大视野，也是一个增进世界各国了解和交流的平台。但是没有必要进行这样大规模的宣传造势以及对市民生活的过多限制，它不应该被当做一个政治任务来抓。去年芝加哥申办2016奥运会时支持率很低，而除此之外它的很多方面都是最优秀的，我并没有因此看到美国的有些部门去上门给芝加哥民众“做工作”甚至进行威胁叫他们支持申奥。我也没有看到上海世博会自我标榜的一些理念付诸实践，诸如什么“低碳世博”、“绿色出行”、“勤俭办博”、“廉洁办博”、“和谐世博”等等。我也去过浦东，它不像日本等其他国家的世博会是在郊区的森林附近举办的，而且黄浦江两岸燃放的烟花比北京奥运还多。现在世博会在很多国家都是鸡肋，在民众连基本的医疗保障和失业救济都没有的时候，我不知道花这么多钱举办这样一个东西对民众有什么意义。尤其是有那么多的人的房屋因为世博被强制拆迁，而得到的补贴又很少，他们许多人选择忍气吞声，还有少数人走上维\权之路却处处遭到打\压，一些人甚至流离失所，难道这些不都是政府的责任吗？到世博会结束，政府再通过卖土地赚钱，很多人要遭受二次剥\削。<br />
<br />
国\保：世博会是我们国家崛起的一个巨大的标志，我们应该感到骄傲。你刚才所说的这些是从哪里知道的？<br />
<br />
我：海外的新闻媒体吧,BBC、VOA、RFI、DW、RFA、《纽约时报》什么的，还有像国内可以打得开的中文网站，如新加坡《联合早报》。我从来都不信任国内的媒体，毕竟来说，西方的媒体至少是独立的，这可以保证它的公信力，这也是西方媒体能成为有很多受众的全球性媒体的很重要的原因。<br />
<br />
国\保：你英文不错吧？我也经常看海外的一些新闻网站，他们写的许多东西都不可信，很喜欢写些中国的阴暗面，我们应该更多看到我们国家强大积极的一面。<br />
<br />
我：我英文一般一般，也只能说是在了解有关背景的基础上看懂大概的意思而已。新闻媒体本身就应是独立的，它的本质是对社会起监督作用，不是说像中国一样用来宣传的。我们的社会需要进步，要进步就应该去正视和揭露这个社会的阴暗面，而不是去掩盖它，营造一种虚假的和谐。当然西方的媒体是比较客观的，他们有的时候也会对中国表示赞美，比喻对北京奥运的盛大开幕式，这也是中国政府乐于听到的东西。<br />
<br />
<br />
国\保：他们的媒体也有自己的立场，并不是很客观。<br />
<br />
我：任何人都会有自己在一些问题上的立场和价值观，这并不带表就不就客观了。我认为言论足够的自由就可以保证新闻媒体的客观性。<br />
<br />
国\保：我们国家也有言论自由，但是不能说有绝对的言论自由，你不能造谣诽谤别人，不能侵害他人的自由，不可以泄露国家机密。<br />
<br />
我：我从来不认为有什么绝对的自由，自由是指宪政基础上的自由，你当然不能侵犯别人的自由，不可以针对有人格行为的主体进行攻击和辱骂。而政府和总统是不具备主体人格的，也就是说我可以在白宫外面大声的骂美国政府，在电视上公开的批评这个政府，即便是对政府的不实攻击，那么也不应该承担任何法律上的责任。至于泄露国家机密，除非我是某个单位的，我和政府签订了保密协议，那么我泄露了我才会承担法律责任。泄露国家机密应该有一个具体的程序，它不是一个非常宽泛的概念，普通公民无从去知道国家机密，即便知道了，那就一定是政府或者签订了保密协议的人和单位给泄露了，那么签订了协议的人应该被追责，政府应该为失责行为对民众承担责任，普通公民没有所谓的保守国家秘密的义务，更不应该以此为手段对普通公民进行莫须有的打\压。<br />
<br />
国\保：你没看到我们国家这些年的言论已经很进步了，相比十年前，很多东西媒体上都会写，像你说的暴力拆\迁，互联网也是开放的。<br />
<br />
我：现在媒体上会写这些东西，很大程度上是因为有互联网的竞争，而且有些有责任的媒体也是在试探底线，同事要考虑到市场，因为人们不喜欢听过多的吹嘘的声音。很多东西因为是首先在互联网上曝光了，传统媒体然后去进行舆\论导向，相比以前这也算是一种不得已的进步了。政府它不可能完全去关闭互联网，因为它必须去兼顾商业利益，既要完全封住人们的嘴，又想兼顾商业利益，这不可能做到，二者不可兼得。就像之前的这个谷歌事件的发酵就是一个很好的例子。<br />
<br />
<br />
国\保：这个谷歌是怎么回事？<br />
<br />
我：它是因为不堪忍受中国繁琐和不透明的网络审\查制度。导火线只是因为一些维\权人士的 Gmail邮箱遭到黑\客攻\击和谷歌的密码系统被侵\入。谷歌中国本身就背理了谷歌的价值观。<br />
<br />
<br />
国\保：你说的这些是谷歌说的吧，你怎么就信他们说的？<br />
<br />
我：我觉得有些事实摆在眼前，我也不可能去进行调查，相比国内的一些网络企业，我始终相信谷歌，欣赏它提供的服务，没有足够的信誉它是不可能成为全球性企业的。<br />
<br />
国\保：你知道美国国土安全部对全国的电话和网络实行监\听吗？美国人其实不自由，你没去过美国，你就感受不到这些。它的很多东西是和美国宪法相抵触的。<br />
<br />
我：这个我还真不确定，虽然我看过有些国内的媒体有这方面的报道，但是国内媒体的公信力有多少呢？我在其他媒体上从未听到过说美国人的生活全部被监\控，国内的一些人去了美国也没有这方面的说法。美国本身是一个很开放的法治国家，即便有监\听，我相信只是在不侵犯合法公\民的权益的基础上，比喻说对待恐怖分子。<br />
<br />
国\保：你听说过美国最近的广场炸弹袭击的事情吗？那他们是怎么发现的？<br />
<br />
我：这个事情我知道。那是时报广场的一个小贩发现可疑车辆然后报警的。<br />
<br />
<br />
国\保：那美国怎么那么快就把那个人逮捕了呢？美国现在是2亿多人吧（我插话“三亿多”），美国人的信息都被国家所掌控了。<br />
<br />
我：那个巴基斯坦裔美国人是在去机场搭机前往迪拜进行安检时被逮捕的，这说明了美国行政、司法等的高效，美国公民的信息记录在册并不代表美国人的隐私受侵犯，那是美国人的尊严的体现，国家有保密的义务，安全问题同样重要，而对于嫌疑犯美国也是表示充分的尊重。<br />
<br />
国\保：美国发达是因为美国的多元，而你只看海外的东西，偏听则暗，我们在说的时候，你有的时候插嘴，不喜欢听别人的意见，你太一元化了。而且我们国家人口多，这是一个国情。<br />
<br />
我：确实，美国发达是因为有多远的体系，而中国则是一元体制，所以我今天才会来到这里。我不是一元化，我有我基本的是非观念，而中国往往是没有是非观念，我希望任何问题都可以公开讨论，我充分尊重每一个人表达的权利，不管他们持什么立场和思想观念。我接受的信息也是多元的，我并非听到的都是批评的声音，我也比较喜欢温\家\宝吧，但我不喜欢胡\锦\涛。<br />
<br />
国\ 保：你也喜欢温总理。<br />
<br />
我：我认为温\家\宝在共\产\党内面是一个异数，不搞拉\帮结\派，讲了很多启发人的智慧的话语，先不论他的家族，在共\产\党这样的生态里面，这些已经足够了。<br />
<br />
国\保：你要知道网上的言论有很多不真实的。前不久网上流传说，武汉的东湖说要部分填了修建酒店，这个东西不断传，造成了很不好的影响。<br />
<br />
我：这个事情我听说过。如果是这样，那这里政府是不是有很大的责任?中国的政\府都习惯于暗\香操\作，政务不公开，有的时候当然会有流言。如果政务公开了，即便有流言了，也是不堪一击。<br />
<br />
国\保：你身边的人像你这样的思想的多吗？<br />
<br />
我：几乎没有吧。可能是大家从小接受这样一种教育的原因吧，像什么标\注答案，作文要有正确的思想导\向这些东西，它扼杀\人的思想和独立思考能力。我很反对中国现行的教育体\制，我希望能有真真的公民教育，不要再有这样的党\化教育和大学的行\政主导一切。<br />
<br />
国\保：你说公民教育啊，你应该看到我们国家这个良好的政治制度是关键，这个不能改。<br />
<br />
我：我觉得中国的许多问题恰恰就根源于这个政治制度。我知道美国也有贪\污腐\败的问题，这是人性方面的原因，但是它和中国的贪\腐很不相同，美国有一个制约机制，而中国的官\员几乎是人人都贪而且数额巨大。<br />
<br />
国\保：我们不说这个贪\污的问题。你应该看到我们国家现在在不断的强大，而你思想太过于阴暗，只盯住那些不好的方面，偏听则暗，你应该多看看我们国家的主流。<br />
<br />
我：我不认为我所关注的是这个国家的支流。我们的报刊媒体上到处都是一些把政府赞美得天花乱坠的东西，这个社会需要更多批评的声音来推动社会的进步，我希望我们的国家是一个自由和开放的国度，像美国一样，可以容纳各种不同的声音。<br />
<br />
国\保：我们国家这些年来在不断的进步，这就是主流。你感觉到我们国家这些年来进步了吗？<br />
<br />
我：没有明显的感觉，我更多的感觉是退步了。<br />
<br />
国\保：那你觉得是那些方面的问题?<br />
<br />
我：这些年来不断上演的暴力拆\迁的问题，政府为了卖地赚\钱利用权\力配合黑\社会来抢\劫民\宅，只有低价的补 \偿，很多人上\访却要被关\押。还有环境污染和各种安全事\故愈演愈烈。基本的社会保障欠缺，很多人生病了可能不得不等死。劳工的权益得不到保障。物价飞涨，工资却不见上涨。官\商勾\结，压\榨私\营企业，然后企业又把这些压力转\嫁给劳工，也就是国进民退的问题。总之，很多很多问题。<br />
<br />
国\保：你说的这些是民生问题，这些确实存在，但我们不也在进步吗？比喻这个拆\迁的问题就在进行重新立法来阻止暴\力拆\迁。<br />
<br />
我：我不认为这个问题重新立法了就可以解决这个问题，只要政府性\质不变，那么这样的问题就难以解决。<br />
<br />
国\保：那你说说我们国家在哪些方面有进步？<br />
<br />
我：进步还是有的吧，比喻说获得了这个奥运会和世博会的主办权，北京的空气质量有改善，经济在不断增长，还有就是在国际上建立了那么多孔子学院进行文化输出。总之，在经济文化方面取得了一些进步。（不知道这是否算进步，他们问，我也只能这么回答。）<br />
<br />
国\保：你说的这些还不够准确。我们的进步更多的体现在我们国家的政治越来越优化，党和国家解决了许多问题。我们获得了国际上越来越多的赞美，地位更高了。<br />
<br />
我：我可真的没有像你们这么明显的感觉。可能因为我只是游走于体制外的社会底层的人士，不像你们生活在体制内，可以亲自去体会那种“进步”。当然我知道你们今天来也是奉上。<br />
<br />
国\保：这不是体制的问题，你看问题太过于阴暗。看问题要坚持客观和理性。<br />
<br />
我：我觉得我一直都很客观理性，我并不会为了批评某些东西而去造谣什么的，我热爱真理。也许我以后该更加的客观理性。<br />
<br />
国\保：我向你提出三点要求，希望你记住：1、不要再写那些文章了；2、要坚持客观、理性、准确、全面的看问题；3、希望你找到一个好工作，现在先要谋取生存，有些东西不需要你操心。<br />
<br />
我：我记住了。<br />
<br />
国\保：进入社会后，你会发现还有很多个人问题要去解决，你的思想也会慢慢改变和成熟。<br />
<br />
我：进入社会后，我一定会发现中国社会比校园还要黑暗得多。怎么变得更成熟？也就是更世俗化吧，我不知道是否还要坚持原则和价值理念。<br />
<br />
国\保：我也经历过你这个阶段，当时我比你还激进。就像小时候做的某些事，在现在看来是多么的幼稚。不要总是举着民主、人权的大旗，就以为站在了制高点，听不进去他人的话。<br />
<br />
我：每个人的经历和价值观念有差异，我并没有这么深的感受。也许我会改变吧，但我始终希望我们的社会越来越自由和开放并愿意为此付出努力，我更希望我作为一个个体能够得到平等的尊重。<br />
<br />
国\保：以后有问题可以多和这位书记交流一下。<br />
<br />
我：我在学校的时间不会很长了，应该没有这个必要了。<br />
<br />
国\保：记住我们的话，你可以走了。<br />
<br />
我：谢谢。<br />
<br />
整个“思想教育”过程持续了一个小时左右，期间党委书记不时的给我和几个国\保倒茶并劝我喝茶。谈话过程中，说话最少的那个国\保应该是在进行记录，内容可能是关于我的个人和家庭信息还有就是所谓的“思想动态”的要点，我和国\保的谈话极有可能被录音了。因为我没有进行录音，以上谈话只是大概版本。期间他们还问了关于我的个人和家庭信息等其他问题。<br />
<br />
我不知道他们是如何获取我的个人信息，然后在学校的配合下找到我的。然而，我对此一点都不感到惊讶，我无法揣摩他们的神通广大。在这样的一个特\务遍地和告\密成风的国度里，你永远都不会感到孤独，因为你根本无法使你的有关个人隐私获得基本的保障，随时都可能有人亲自上门来“关照”你，让你猝不及防。“盛世和谐”便是这样被打造的。<br />
<br />
“软硬兼施”是一门生存斗争哲学。我这一次能享受此特殊“礼遇”只是一记胡萝卜式的警告，它预示着敢于质问和批评政府的人将受到惩罚。如果还有下一次，那就一定不会这么幸运了，直接用“大棒”伺候。我并不希望有下一次，在未来一些时候，我可能不得不“闭嘴”，我无法用一个人的力量去对抗整个国家机器。在现实中国，“要么服从，要么闭嘴”这是我们每一个人必须面对的选择，如果有第三种选择，那一定是死路一条!<br />
<br />
我开博的时间不长，但是在这里我收获了很多，收获了知识和友谊，感谢各位光临过我博客的博友，也感谢新\浪，尽管它不断的删我的文章和封杀我的博客。我不知道胡萝卜式的警告是否就意味着它剥夺了我的博客写作的权利，但是我分明感觉到了我现在的处境很不安全，考虑到我的家庭还有个人的未来，我不得不小心翼翼的行事。万一我在未来不幸“出事”了，我希望朋友们不要认为什么事情都没有发生，我并不是想出名，只是因为我不想承受任何的不白之冤，对不起，请原谅我的软弱。我不想说这是我最后一篇时政类博文，但极有可能的是我很长时间都不会再更新博客或写时政类博文了，除非中国的言论环境在短期内能够获得改善，但谁都知道这只能是妄想和扯谈。我并不想昧着自己的良心去为剥削我们的人唱赞歌，即便中国某天变得民主了，我也不会成为一个歌颂者。今后在积极融入社会的同时，我依然会坚持自身的理想和价值信念。<br />
<br />
和大多数人一样，我也有最原始的生活向往：我希望能够拥有自己安居的家庭；能够早日遇到自己的另一半；能够和自己的另一半共享二人世界的天伦之乐；能够孝敬自己的父母；能够与朋友和亲属分担和分享酸甜苦辣；能够看到自己的后代不要活在谎言之中和获得健康的成长；能够不为粮食和蔬菜发愁；能够避免因病而拒绝被医院接纳的遭遇；能够不受到户籍歧视......我更希望在我死后能够有人为我免费收尸并获得一个安身之地。如果有可能，我希望通过自身的努力可以离开这个国家，并为自己不再拥有中\华人民共\和国的国籍而获得美利坚、不列颠、法兰西、德意志、澳洲、纽西兰或中华民\国的绿卡或国籍而深感荣幸，到一个自由的国度里去安居。在那里司法是独立和公正的，法治健全，不会“因言获\罪”，也不会被请去“喝茶”，没有莫须有的罪名,没有狂热的民族主义,可以自由的出入境，没有防\火\墙，可以自由的上Twitter、YouTube、Facebook和Google，不会因为“犯错”而被要求作 “思想检讨”和定期的“思想汇报”甚至因此遭遇就业歧视，个体的尊严会得到平等的尊重，生老病死会有保障......<br />
<br />
<br />
“为什么我的眼里常含泪水，因为我对这土地爱得深沉。”和艾青以及他儿子艾\未\未一样,我也深爱着这片生我养我的土地。我和LXB有着一样善良和普世的愿望：我期待我的国家是一片可以自由表达的土地，在这里，每一位国民的发言都会得到同等的善待；在这里，不同的价值、思想、信仰、政见......既相互竞争又和平共处；在这里多数的意见和少数的意见都会得到平等的保障，特别是那些不同于当权者的政见将得到充分的尊重和保护；在这里，所有的政见都将摊在阳光下接受民众的选择，每个国民都能毫无恐惧的发表政见，决不会因为发表不同的政见而遭受政\治\迫\害......言论自由是一项极端重要的核心权利，正如LXB所述的那样：“表达自由，人\权之基，人性之本，真理之母。封杀言\论自由，践踏人\权，窒息人性，压抑真\理。”我也深切的期望台湾的民\主能够引领中国大陆早日走上自由、平等、均富的大道!<br />
<br />
现在，我基本上已经毕业了，获得一份工作对我而言显得极为紧迫和重要。想起当初父母期待的目光和现在焦急的眼神，我有一种心如刀割的感觉。我确实有一点后悔在这些年里我没有能够好好学习，所以现在找工作总是碰壁。我只是华中一所非常一般的院校的普通学生，我也见证了在象牙塔这片“净土”里的一些黑幕，也许还有很多中国特色的黑幕和潜规则是我无从知晓和经历的，它们像魔咒一般侵蚀着老师、学生、各级官员的灵魂，这片“净土”早已在中国变得污浊不堪。曾经的我幻想可以一直读下去，现在这种天真的愿望已经被无情的现实所幻灭，我无心再呆下去了。现在我没有经济能力或者实力和机会出国深造和进修，这不能不说是一种悲哀，我不能去埋怨任何人，但愿未来可以有弥补的机会吧。为了能够先行解决糊口和生存的问题，我现在得暂时搁置心中的理想主义而回归现实主义，但是理想主义的火种会激励我一直前行，我期待着下一次再次拥抱她，我相信梦想终究会照进现实。明天，我该再次出发了，收起行囊加入到正在找工作的待就业大军之中，不知道广州或其他城市哪里会有我的容身之地呢?我真的感到很焦虑与迷茫......<br />
