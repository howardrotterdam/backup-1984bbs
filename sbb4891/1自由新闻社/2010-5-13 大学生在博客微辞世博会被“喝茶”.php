“软硬兼施”是一门生存斗争哲学。我这一次能享受此特殊“礼遇”只是一记胡萝卜式的警告，它预示着敢于质问和批评政府的人将受到惩罚。如果还有下一次，那就一定不会这么幸运了，直接用“大棒”伺候。我并不希望有下一次，在未来一些时候，我可能不得不“闭嘴”，我无法用一个人的力量去对抗整个国家机器。在现实中国，“要么服从，要么闭嘴”这是我们每一个人必须面对的选择，如果有第三种选择，那一定是死路一条!<br />
