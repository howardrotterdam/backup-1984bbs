													<h2>上海世博会十宗罪 转</h2>
						
						
																											<div id="postmessage_614412" class="t_msgfont">自从2002年12月3日中国上海取得了2010年世界博览会的主办权之后，大大小小的折腾就从未消停过，中国每一次获得大型盛会的主办权似乎都带来了灾难。包括奥运，强迁了一批居民，甩了大把银子；世博，又强迁了一批居民，甩了大把银子。我们感到奇怪的是，奥运本来是一种体育精神，在任何一个国家，都不会当作一个政治任务来抓。世博会原本是世界各国各地区展示科技创新、产业创意的成果的一个展览会，它的底色理应是文明、祥和，而不是闪现刀光剑影。但是在专制国家相反，什么事都要上纲上线到政治的高度，泛政治化往往成为一种思维定势。上海世博被当成全国的一个政治任务，这种导向不可避免的产生了许多问题。与历届世界博览会一样，中国2010年上海世博会也有自己的主题：“城市，让生活更美好”。然而，这一届世博会的实情却是“城市，让生活更糟糕”，竟给那么多人带来不幸、痛苦或麻烦。如此兴师动众、劳民伤财、气氛紧张，这到底是为哪般？说实话，上海世博会对P民基本上没有任何好处，顶多只能满足一些人的虚荣心和激发他们狭隘的民族主义。再就是，如果你有钱而且不嫌麻烦可以去现场参观一下和接受一下启蒙教育当然也不错。距2010年上海世博会举办日期仅剩一周的时间，当地的各项准备工作正在紧锣密鼓地展开，不过近日上海世博会试运行场面一片混乱，差点被挤垮。下面是经过综合整理然后列举的上海世博会十宗罪。<br />
<br />
 <br />
<br />
世博难民DD因为筹办世博会<br />
<br />
&nbsp; &nbsp; 黄浦江附近许多居民曾被迫迁移，更有许多居民遭到强行迁移，以至流离失所。随着世博会，产生了“世博难民”，他们中还有一些人被关、被打、甚至丧命，生存状况堪忧。中国改革开放三十年来，取得的成果就是产生了两千万上访大军。建立在低人权和对资源环境的掠夺与破坏基础上的高速经济增长造就了庞大的利益集团，权贵的利益在不断发酵，他们分享到了改革开放的果实，民众却只能享受经济发展的荣光，底层的老百姓更一直在受难，受迫害。<br />
<br />
 <br />
<br />
侵犯人权DD上海世界博览会逼近<br />
<br />
&nbsp; &nbsp; 上海当局为严控在当地工作的逾十万名外国人，要求他们每晚必须11时前返回住处，引发部分外国人不满，认为严重侵犯人身自由和个人私隐。而且在和平时期的盘问、搜包、验证，这已然是对人的尊严的冒犯，对人权的侵犯。一些维权人士和政治异见人士也将很难避免因世博受到骚扰和监视,就在最近，上海维权人士冯正虎宣布举办“冤案博览会”后，表示遭到警方登门“抄家”，带走了电脑、上网数据机等器材。此外上海世博会不设示威区，而在中国又无法自由的游行、抗议，也就是说世博会期间禁止示威游行，这将严重侵犯在沪中外公民的游行、示威、抗议和表达的权利......许多迹象似乎都显示:和北京奥运一样，世博会不仅不会促进中国人权的改善，反而会使中国人权状况变得更加糟糕，国际上对中国人权改善的期待将再次落空。<br />
<br />
 <br />
<br />
好大喜功的面子工程<br />
<br />
&nbsp; &nbsp; 由国家主办上海承办的世博会的花销为4000亿，成为史上最昂贵的世博会，与勤俭办博的理念背道而驰。再看看，西南五省大旱，6000万人受灾，损失200多亿,中央拨付旱灾救灾资金1.5亿元；“两会”期间，有官员指出:中国实现全民免费医疗每年需花费1600亿元,目前中国不具备这个经济实力!对比一下，民生工程在面子工程面前显得多么的脆弱。花费重金办世博完全就是不顾国内人民死活，只是当局往自己脸上贴金，标志着国进民退的状况进一步恶化。而上海世博会被当作政绩工程亦极有可能成为一些官员炫耀的资本和升官的阶梯，P民们只能憋一肚子气。<br />
<br />
 <br />
<br />
不透明的财务预算和闭门决策<br />
<br />
&nbsp; &nbsp; 上海世博会的开支明细至今未向社会公开，决策习惯闭门决策，毫无民主成份可言，不符合廉洁办博的原则，这是对纳税人的知情权的剥夺，严重侵害纳税人的合法权利。<br />
<br />
 <br />
<br />
文化损毁与房价畸高<br />
<br />
&nbsp; &nbsp; 上海当局为筹办上海世博会，使得一片片有上海特色的老城区被推土机和大铲车夷为平地。建成后的绝大部分场馆将在世博会后拆掉，不全拆是为了保留“世博板块”用于带动房价的飙升，房价将更加畸形，政府和开放商从中渔利，并将造就少数炒房客和大批的房奴，民众生存状况难免更趋恶化。<br />
<br />
 <br />
<br />
扰民会DD上海当局的限制范围不断扩展，上海市民对这种剑拔弩张的态势怨声载道。<br />
<br />
&nbsp; &nbsp; 当局以改善市容保障安全为由，除了限制民众晒衫，又劝阻民众穿睡衣上街，买菜刀也要实名制，上海市民被越来越多的约束规定所烦扰，认为世博干脆改名为“扰民会”。上海当局近日又宣布，居民购买老鼠药及含剧毒成份的农药物品，也要凭身份证，要“实名制”。同时像地铁安检等严格的交通管制和长年累月的施工也对出行造成了极大的不便......人家美国已经举办过14次世博会了，没见过有哪一次是像中国上海这般神经紧绷的。这些举措显露了公权力介入公民私生活的苗头，政府机构应该是公共性格的示范，而不是具体公民公共性格的强制塑造者。<br />
<br />
 <br />
<br />
礼仪小姐变态培训<br />
<br />
&nbsp; &nbsp; 伴随上海世博会的日益临近，各馆礼仪人员展开全方位培训。来自各地高校的数名美丽可爱的姑娘们经过层层选拔，接受形体、外事礼仪、世博会知识、外语等方面的培训，最终确定为礼仪人员。但如此训练方式，真是让人不免心生怜惜。礼仪不是这样练成的！这么练是叫这些女孩子去卖笑么？我就不明白了？就不能像其他国家的女孩子一样？发自内心的微笑么？<br />
<br />
 <br />
<br />
世博歌曲抄袭事件<br />
<br />
&nbsp; &nbsp; 距离上海世博开幕还有两个礼拜时，惊爆出“抄袭门”事件，世博推广歌曲《2010等你来》涉嫌抄袭日本歌手冈本真夜所唱的《不变的你就好》，听上去除了中日文歌词不同外，曲调非常类似，相似度百分之九十以上，再次将天朝模仿和窃取的本能发挥到极致。未开幕，先丢人，这个估计动用了一千万人民币制作费的世博曲现在看来成了上海世博会的烫手山芋，也实为一大国耻。独立评论人朱建国说：“山寨文化，它实际上是中国民间对政府的一种过度管理、专制的控制的一种变相的反抗。因为它有很多民间的文化，它不能够通过正常的途径，得到表达。所以，民间就会采取很多方式，来制造一种山寨文化，来表达他们自己的意见。”确实，要怪只能怪中国这病态的政治。<br />
<br />
 <br />
<br />
城市，让生活更糟糕<br />
<br />
&nbsp; &nbsp; 上海世博会的口号“城市，让生活更美好”不够好，应该是“城市，让美好的生活更美好”。中国的大城市就是这样，毁灭100万个理想，用这100万个理想诞生出1到2个新富。这两年大学扩招了，我并不奢望大学扩招能多培养人才，我觉得可以稀释大学生很脑残的萌动，从学校出来的学生是很可怜的。现在的大学生真的很幸福，因为他们出来的时候并不是忙自己的事业，更多的是被房子所困扰。一方面房子替代了户口，一方面上海这个城市不能给人安全感。在这个地方生活下来不是很容易，随着世博会的临近我们的生活成本越来越贵，出租车是13块钱起步，地铁是3块钱起步。我认为上海不是一个真正大都市，因为我觉得一个真正的大都市是可以安居乐业的。上海政府把上海当成冒险家的乐园，冒险家的乐园虽然好听，但是我不赞成。我觉得这样的城市并不适合居民居住，这也是把为人民服务当成养猪做。我是一个很可怕的税民，我有一个希望，有一天我不小心死掉了，我希望政府能免费捞我的尸体。(韩寒)<br />
<br />
 <br />
<br />
“太平盛世”下被掩盖的罪恶<br />
<br />
&nbsp; &nbsp; 中国当局希望每通过举办一些大型盛会来对内凝聚民心对外改善国际形象，可是他们的愿望总是无法实现，或者说会大打折扣，2008年为了举办北京奥运，早在举办奥运之前就已经被发现的三聚氰胺毒奶粉因为要“和谐”办奥运而一直推迟到奥运结束快一月时才曝光，当局试图通过奥运会来赢得国内国际的认同的努力遭受沉重打击。同样西南的旱灾早从去年底就已经出现，但是因为春节和三月份劳民伤财的“两会”政治秀，西南干旱直到三月下旬才为媒体所广泛关注，贻误了多少宝贵的救灾时机。而上海世博会将长达半年，世博会结束紧接着是广州亚运，这么长的时间里，很难说“太平盛世”之外不会发生任何的公共危机，会不会为了“和谐”，“太平盛世”下的罪恶再次被掩盖或贻误曝光的时间呢？<br />
<br />
 <br />
<br />
&nbsp; &nbsp; 我并不反对世博会本身，但是反对被政治化的世博会。最后，还是期望世博会能够早日的安然落幕，但愿将来不要再有那么多劳民伤财和恐怖森严的政治浮世绘。</div>

							
							
							
							
