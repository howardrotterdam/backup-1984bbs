<br />
<br />
国\保：那美国怎么那么快就把那个人逮捕了呢？美国现在是2亿多人吧（我插话“三亿多”），美国人的信息都被国家所掌控了。<br />
<br />
我：那个巴基斯坦裔美国人是在去机场搭机前往迪拜进行安检时被逮捕的，这说明了美国行政、司法等的高效，美国公民的信息记录在册并不代表美国人的隐私受侵犯，那是美国人的尊严的体现，国家有保密的义务，安全问题同样重要，而对于嫌疑犯美国也是表示充分的尊重。<br />
<br />
国\保：美国发达是因为美国的多元，而你只看海外的东西，偏听则暗，我们在说的时候，你有的时候插嘴，不喜欢听别人的意见，你太一元化了。而且我们国家人口多，这是一个国情。<br />
<br />
我：确实，美国发达是因为有多远的体系，而中国则是一元体制，所以我今天才会来到这里。我不是一元化，我有我基本的是非观念，而中国往往是没有是非观念，我希望任何问题都可以公开讨论，我充分尊重每一个人表达的权利，不管他们持什么立场和思想观念。我接受的信息也是多元的，我并非听到的都是批评的声音，我也比较喜欢温\家\宝吧，但我不喜欢胡\锦\涛。<br />
