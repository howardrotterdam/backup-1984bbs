我不知道他们是如何获取我的个人信息，然后在学校的配合下找到我的。然而，我对此一点都不感到惊讶，我无法揣摩他们的神通广大。在这样的一个特\务遍地和告\密成风的国度里，你永远都不会感到孤独，因为你根本无法使你的有关个人隐私获得基本的保障，随时都可能有人亲自上门来“关照”你，让你猝不及防。“盛世和谐”便是这样被打造的。<br />
