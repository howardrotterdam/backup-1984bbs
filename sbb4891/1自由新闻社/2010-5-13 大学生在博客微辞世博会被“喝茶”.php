我开博的时间不长，但是在这里我收获了很多，收获了知识和友谊，感谢各位光临过我博客的博友，也感谢新\浪，尽管它不断的删我的文章和封杀我的博客。我不知道胡萝卜式的警告是否就意味着它剥夺了我的博客写作的权利，但是我分明感觉到了我现在的处境很不安全，考虑到我的家庭还有个人的未来，我不得不小心翼翼的行事。万一我在未来不幸“出事”了，我希望朋友们不要认为什么事情都没有发生，我并不是想出名，只是因为我不想承受任何的不白之冤，对不起，请原谅我的软弱。我不想说这是我最后一篇时政类博文，但极有可能的是我很长时间都不会再更新博客或写时政类博文了，除非中国的言论环境在短期内能够获得改善，但谁都知道这只能是妄想和扯谈。我并不想昧着自己的良心去为剥削我们的人唱赞歌，即便中国某天变得民主了，我也不会成为一个歌颂者。今后在积极融入社会的同时，我依然会坚持自身的理想和价值信念。<br />
