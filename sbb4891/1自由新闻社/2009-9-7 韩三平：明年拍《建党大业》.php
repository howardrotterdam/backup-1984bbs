准备补发演员片酬<br />
<br />
被问及票房期待时，本地院线负责人赵军称广东票房将达5千万，听到这个数字韩三平面露喜色：“广东有5千万的话，全国票房就4.5亿以上了。”<br />
<br />
提到补发片酬，韩三平说“当然要给”：“李连杰、成龙有自己的基金会接收补发的片酬，像陈坤这样没有建立基金会的演员的片酬，全部放进宋庆龄基金会。”导演黄建新则表示，《建国大业》明年在台湾上映，将分开上下集甚至4集发行《建国大业》影碟，届时将加上许多删减的戏份。<br />
<br />
《建党大业》2011年上映<br />
<br />
韩三平当天承认正在筹拍《建国大业》前传，而且明星不少于《建国大业》。韩三平对本报记者表示，筹拍前传的原因是他觉得跟建国时期相比，中国共产党诞生的时期也是中国一个重大历史时期，“1917年十月革命一声炮响，给中国送来了马克思主义，到1921年南湖组建中国共产党，那个时期也有同样精彩的历史人物，战争、爱情、阴谋等等元素都有，而且也同样具有献礼的意义。”<br />
<br />
