核心提示：导演韩三平宣传时承认正在筹拍《建国大业》前传《建党大业》，而且明星不少于《建国大业》，该片有望2010年上半年开拍，2011年上映。<br />
<br />
《建国大业》有一场之前甚少提到的“主席遇袭”戏。（CFP图）<br />
<br />
广州日报9月16日报道 《建国大业》导演韩三平、黄建新率演员邬君梅、陈坤、王学圻、刘仪伟等到广州宣传该片。在将该片全国票房期许提到4.5亿同时，韩三平当天承认正在筹拍《建国大业》前传，“姑且暂定名《建党大业》”。《建党大业》将成为建党90周年献礼片于2011年上映，明星更多。<br />
