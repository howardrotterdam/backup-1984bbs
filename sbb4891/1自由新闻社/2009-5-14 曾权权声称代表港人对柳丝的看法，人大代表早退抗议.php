&nbsp; &nbsp; 分裂国家罪的定义是藉使用严重危害中华人民共和国领土完整的武力或严重犯罪手段，或藉进行战争，将中华人民共和国的某部分自其主权分离出去。“抗拒行使主权”的提述将在分裂国家罪中删除。 <br />
<br />
<br />
&nbsp; &nbsp; 颠覆 <br />
<br />
<br />
&nbsp; &nbsp; 颠覆罪是指藉使用严重危害中华人民共和国稳定的武力或严重犯罪手段，或藉进行战争(1)废止《中华人民共和国宪法》所确立的国家根本制度；(2)推翻中央人民政府；或(3)恐吓中央人民政府。 <br />
<br />
<br />
&nbsp; &nbsp; 分裂国家罪和颠覆罪中，“威胁使用武力”的提述将会被删除，只有真正涉及战争、武力或类似恐怖活动的严重犯罪手段才会被涵盖。 <br />
<br />
<br />
&nbsp; &nbsp; 煽动叛乱煽动叛乱罪是指：(1)煽惑他人犯叛国、颠覆或分裂国家的罪行；或(2)煽惑他人进行严重危害中华人民共和国稳定的公众暴乱。 <br />
<br />
<br />
&nbsp; &nbsp; 有关处理煽动刊物的罪行，控方将须毫无合理疑点地证明有关人士的确具有煽惑他人干犯叛国、分裂国家或颠覆罪的意图。 <br />
<br />
<br />
&nbsp; &nbsp; 现行法例中有关管有煽动性刊物的罪行会被废除。言论、出版及学术研究自由均充份受到保障。 <br />
<br />
<br />
&nbsp; &nbsp; 窃取国家机密 <br />
<br />
<br />
&nbsp; &nbsp; 只会对现行的《官方机密条例》进行两项轻微修订。 <br />
<br />
<br />
&nbsp; &nbsp; 对于有关保护“中央与特区关系”的资料方面，受保护的资料只限于与香港特区有关并且根据《基本法》是由中央负责的事务的资料。此外，如披露该等资料会或相当可能会危害“国家安全”，有关人士才会被惩处。“国家安全”在现行的法例中的定义为“保卫中华人民共和国的领土完整及独立自主”。经济资料的自由流通，将不会受影响。 <br />
<br />
<br />
&nbsp; &nbsp; 第二项修订是为了堵塞有关以非法手段取得受保护资料并作出损害性披露的漏洞。 <br />
<br />
<br />
&nbsp; &nbsp; “违法取览”限于藉电讯而在未获授权下取用电脑资料、有犯罪或不诚实意图而取用电脑、盗窃、抢劫、入屋犯法或贿赂这些在香港法律下的刑事行为。 <br />
<br />
<br />
&nbsp; &nbsp; 外国政治组织 <br />
<br />
<br />
&nbsp; &nbsp; 现行《社团条例》的条文禁止外国的政治组织或团体在香港特区进行政治活动，及禁止香港特区的政治组织或团体，与外国的政治组织或团体建立联系。 <br />
<br />
<br />
&nbsp; &nbsp; 条例草案不对有关条文作出修订。 <br />
<br />
<br />
&nbsp; &nbsp; 取缔危害国家安全的组织《社团条例》已经赋权保安局局长取缔危害国家安全的组织。在行使该项权力时，须按照《公民权利和政治权利国际公约》有关保护国家安全的准则属必须和相称的标准，条例草案赋予保安局局长有相类的权力禁制危害国家安全的组织，但该新增权力只可在符合下列其中一项先决条件下方可行使─(1)有关组织的宗旨或其中一项宗旨是从事叛国、颠覆、分裂国家、煽动叛乱或谍报活动；(2)有关组织已作出或正企图作出叛国、颠覆、分裂国家、煽动叛乱或谍报活动；或(3)有关组织从属于某内地组织，而该内地组织已被中央人民政府藉明文禁令并按照国家法律，以保障中华人民共和国安全为理由禁止运作。 <br />
<br />
<br />
&nbsp; &nbsp; 某本地组织被视为“从属”于一内地组织，是指前者接受后者可观的财政资助、前者受后者指示或控制，或前者的政策由后者厘定。 <br />
<br />
<br />
&nbsp; &nbsp; 即使有本地组织从属于被中央明令禁制的内地组织，该本地组织也不会自动被禁制。保安局局长的决定，必须符合国际人权公约的标准。 <br />
<br />
<br />
&nbsp; &nbsp; 任何对保安局局长取缔某组织的决定不满意的人士，无论是关于事实或法律观点，均可向原讼法庭提出上诉。而有关特设上诉审裁处的建议会被删除。 <br />
<br />
<br />
&nbsp; &nbsp; 取缔决定本身并不会引起刑事罪行；除非任何人士在保安局局长作出取缔决定后，仍继续支持该组织的运作，才构成刑事罪行。 <br />
<br />
<br />
&nbsp; &nbsp; 紧急调查权力 <br />
<br />
<br />
&nbsp; &nbsp; 条例草案将会订明只有职级在总警司或以上的警务人员方可授权行使紧急调查权力。只有在警务人员合理地相信有人已干犯或正在干犯有关罪行，而必须立即采取行动，否则调查该罪行的重要证据将会丧失，并因而会对该罪行的调查造成严重影响，方可行使紧急权力。 <br />
<br />
<br />
&nbsp; &nbsp; 条例草案并订明不论在任何情况下，执法人员必须向法院申请手令，方可搜查或检取新闻材料。新闻自由将不会受到影响。 <br />
<br />
<br />
&nbsp; &nbsp; 正如我们早前曾公开承诺，我们不会要求扩大警方的财务调查权力。 <br />
<br />
<br />
&nbsp; &nbsp; 由陪审团审讯 <br />
<br />
<br />
&nbsp; &nbsp; 条例草案将订明被指控干犯叛国、分裂国家、颠覆、或煽动叛乱中煽惑他人干犯叛国、颠覆、分裂国家等罪行的人士，将由陪审员审讯。而被控煽动叛乱中煽惑他人进行严重危害国家稳定的公众暴乱及非法披露罪行的人士，亦可以选择由陪审员审讯。 <br />
<br />
<br />
&nbsp; &nbsp; 符合《基本法》第39条 <br />
