“一定有一些含义的，每一种花都有含义，比如，玫瑰象征着爱情，菊花……”<br />
“哦，那就是美好，纯洁，幸福？”<br />
<br />
“可是这是白菊花啊”<br />
“菊花本来就有白色，黄色，紫色……”<br />
<br />
“白菊花是祭奠用的”她突然抛出了一个理论<br />
“哦？是吗，这可是您说的，祭奠？祭奠什么？”<br />
<br />
阿姨无语了，正好特异功能和他的几个练健身的兄弟上车了，阿姨对他们说她吃饭去了，下车的时候看了我一眼，如同祥林嫂般喃喃的说道“这孩子，跟我儿子一样大。”<br />
<br />
特异功能人走了，留下了健身者，晒得很黑的南戴河爱好者，和一个叔叔。<br />
<br />
南戴河爱好者要走了我的基本信息后下了车，似乎在这群人里地位最高的叔叔凑了上来。<br />
<br />
“带花来广场做什么”<br />
“看降旗”<br />
<br />
“你有两种选择，把花留下，去看降旗，要么带着花离开。”<br />
“也就是说法律禁止人民带着花去看降旗咯”<br />
<br />
“没有，我不是这个意思，我只是执行命令”<br />
“您的意思是您的领导命令您拦住带着花来看降旗的人？”<br />
<br />
“不是，根据你的情况，领导是这么批示的”<br />
“理由是什么，一朵白菊花能伤害谁？似乎不在你们的违禁品名单上啊”<br />
<br />
“没有理由，戒严需要理由吗！”叔叔的耐心和礼貌突然消失了<br />
“广场戒严了吗？没看到通知啊”<br />
<br />
叔叔刚想说话，突然沉默了………………<br />
<br />
“你尊不尊重我，你到底尊不尊重我？”叔叔突然问了一个关于感情的问题<br />
“我很尊重您啊，您有没有发现，我一只在对您使用敬语‘您’，而您一直在说‘你’……”<br />
<br />
“你要是尊重我，就赶快照我说的做”<br />
“我尊重法律，当然，也尊重您，但我不能按您说的做，除非您给我一个法律条文的理由，哪一条禁止人带菊花上街”<br />
<br />
我们还是略去中间与这位警号051911名叫王坤的police叔叔关于纳税人纳税多少才有发言权的谈话和各种上车来好事者问的消磨时间的各种车轱辘话和一个人究竟要不要带身份证出门的重复问题不表，反正我带着对于他们为人民服务的效率低下（一个小时竟然查不出一个身份证号码）和无法按时看降旗的不满拿着白菊花和书随着依维柯（一路逆行）来到了“局里”。<br />
<br />
这个叫“局里”的地方并没有门牌号，而且进进出出有面有菜色的乡下人，一些挺着肚子尾随他们的police，带着耳机在门口晃来晃去的不明身份者，和第四种人，他们不属于上面任何一类，但都靠在墙上斜眼看人，而且都晒得很很黑，我心想还是离他们远点吧，万一是攻击型神经病怎么办。正想着，后面有两个人叫我的名字。<br />
<br />
“进去核实身份”南戴河爱好者指着一个昏暗的楼道说<br />
“你们不是都知道了吗”<br />
<br />
“我知道什么了我？”<br />
“你的POS机几秒钟就能查出我的身份”<br />
<br />
“pos机坏了，网络不通”南戴河爱好者眨了眨眼睛<br />
“不要撒谎，一个大老爷们儿不要撒谎”<br />
<br />
“真坏了，不信您……”南戴河爱好者快哭了<br />
“好好好，行吧”<br />
<br />
我怕他一个大男人真哭了我反而下不来台，就走进了那个卫生搞得十分糟糕的楼道，由健身爱好者尾随，绕过几个带着哭腔的乡下人，进入了一个有趣的房间，这个房间没有任何看上去能核查身份的设备，只有几把椅子，几个摄像头，一张桌子上放着一摞白纸和红色的印泥盒。一扇窗户上有铁栅栏和防盗门。<br />
<br />
“坐吧”健身爱好者指了指一个凳子<br />
<br />
“怎么了？”出现了一个新人物，他年纪不大，三十上下，穿着警服，对健身爱好者说。（就叫他警服小子吧）<br />
“带白花的”健身爱好者抬头看了一眼我。<br />
<br />
“为什么来广场”警服小子微笑着拉了把椅子坐着我旁边<br />
“我希望你们抓紧时间，去核实身份，我只是来看降旗，没带身份证”<br />
<br />
“带白菊花做什么”警服小子还是笑<br />
“喜欢菊花”<br />
<br />
“喜欢菊花?”<br />
“喜欢，家里也养了不少”<br />
<br />
“家哪儿的？”警服小子渐入佳境，此时走进来一个穿军装的干部模样的人，没有说话，只是坐在了健身爱好者的旁边。<br />
“海淀”我答道<br />
<br />
“为什么今天来看降旗”<br />
“因为昨天没时间，明天也不想来”<br />
<br />
“带着花今天来看降旗是不是要纪念什么？”警服小子有点急躁了<br />
“啊？您什么意思，我完全不明白，请您说明白点。”我大惊<br />
<br />
“今天是六月日”警服小子目光如炬<br />
“六月日是什么日子？有什么特别啊？”我直视着他反问<br />
<br />
警服小子一下子不说话了，<br />
“诸位，今天是什么日子，有什么特别？”我站起来大声的问，但房间里没有一个人能回答这个问题，健身爱好者还装作看手机。这时，门开了。<br />
<br />
“你现在是否住在海淀区xxxxxxx”闯进来一个秃头，没打招呼就问。<br />
“不是，我住在xx”<br />
<br />
“详细地址？”秃头掏兜找笔<br />
“我需要先看您的警证”<br />
<br />
“没带在身上，你就说吧”秃头不耐烦的说<br />
“我需要先看您的警证”我重复<br />
<br />
秃头自知违反了操作流程，气喘吁吁的跑回去拿来了警证05114？？ 王xx，因为后面俩字我不认识，是生僻字。我就告诉了他我的住址。<br />
<br />
“单位？”<br />
“没单位，自由职业”<br />
<br />
“父母单位”<br />
“我早成年了，又不是监护人，他们过他们的，我过我的”<br />
<br />
秃头嘟嘟囔囔的走了。<br />
漫长的等待中，看完了半本书。这中间来过两个人，一个是轻微精神病的妇女，自称梦到了某领导人，让她来什么门前面找他。另一个是一个拎着写有“22界教师节留念”的中年男人，很紧张，police让他做什么他就做什么。最后这两个人都被自称是什么什么办的人带走了。<br />
<br />
“人真是奇怪啊！”我放下手中的书大声说<br />
“人有什么奇怪的”健身爱好者和军装干部都看着我<br />
<br />
“随随便便，心甘情愿的就跟一个陌生人走了”<br />
俩人又没说话<br />
<br />
出于无聊，我想起了很多劫机电影里面的经典对话，反正看降旗的时间还早，我就说<br />
“查我身份的人怎么还没回来？”我说<br />
“该回来就会回来的”<br />
<br />
“早就查完了吧，在做什么”<br />
“那不是，赶上了，系统就是这么慢”<br />
<br />
“你有孩子吗？”我对对面的人说<br />
“没有”军装干部笑了<br />
<br />
“你会有的，你有孩子的时候，你将怎么对他们描述自己呢”<br />
军装干部沉默了<br />
<br />
“骗子，你的父亲是一个骗子”我慢慢的说<br />
不知为何，所有的人都不出声的笑了。<br />
<br />
“你还能看书，我只能干呆着陪着你”健身爱好者说<br />
“你想看吗，我给你看”我把书递给他，他摆摆手拒绝了<br />
<br />
“你能把花留下吗，留下你就能走”警服小子插话<br />
“不能，但如果你喜欢，我可以送给你”<br />
<br />
“你能送给我吗”警服小子一脸纯真<br />
“不能，因为我不喜欢你”我嘲讽的看着他<br />
<br />
这时都八点多了，我在这个叫“局里”的鬼地方呆了两个钟头了，陪我逗闷子的只有警服小子，健身爱好者，军装干部三人，查我信息的秃头一去不复返，我对看降旗，实现为它自豪，为它欢呼祝福的念头已经不抱希望了，多么神奇，只因为我没带身份证和一朵花！秃头说来就来，不是一个人来，又带了俩人，活脱从茶馆里第三幕里蹦出来的一样。a,b哥俩一进门儿就横着肩膀。<br />
<br />
“走吧”a说<br />
“去哪？”我问<br />
<br />
“去xxx啊，你不是住那吗，我们就是xxx派出所的”b说<br />
“我自己可以回去”<br />
<br />
“别啊，我们都开车来了，怎么能白跑一趟”a又说<br />
“谁让你们来的，你们自己愿意来！再说，你们是谁，给我看你们的证件”我说<br />
<br />
“没证件，没证件”b突然失控了，口水都出来了<br />
“喊什么喊，没证件你们跑这儿干嘛来了”我看了眼秃头，秃头没说话<br />
<br />
“你也不看看这是什么地方，让你走就走”a说<br />
“你也不看看这是什么地方，你们敢假冒警察，我马上打110”我反问<br />
<br />
“穿着一样的制服，区别真大啊”我对秃头说<br />
秃头转身把两个人拉出了房间<br />
<br />
“你可以走了，你打车走是吧”过了半晌，秃头回来对我说<br />
“这就是本大爷的证件！”a冲了回来，揪着衬衫上自己的警号对我说<br />
“不要激动，041128”我平静的说<br />
秃头又把他拉了出去，对他说“没事了你们走吧”<br />
<br />
“你可以走了，记住，以后如果想献花先去管理处备案”<br />
“谁说我来献花了，我来看降旗”<br />
<br />
“我说如果！”秃头大声说<br />
“我一辈子都不会来献花”<br />
<br />
“那最好”秃头随口说<br />
“你说什么？为什么不献花最好？好在哪？”我没有准备放过他的反动言论<br />
<br />
“当我没说吧，你可以走了”<br />
<br />
我向健身爱好者打了个招呼，感谢他陪我发了一下午呆，他坐在那儿都快哭了，什么都干不了，只能看我，而我一直在看书，也没有要逃跑。练那么多肌肉也没派上用场。几个人起身送我走出了“局里”这个神秘又滑稽的地方。秃头还好心的为我指了能打到出租车的方向。<br />
“再见”<br />
“再见”<br />
<br />
<br />
太奇怪了……派出所的人开车穿越半个北京来送我回家是一种什么程序呢？我的身份到底被核实了没有？为什么我没有在任何文件上签字他们就让我走了？到底今天是什么日子呢？警服小子有什么秘密呢？为何不肯告诉我？难道他们知道什么我不知道的事情？那他既然知道还问什么呢？今天对于他竟然是这么特别，但原因又是什么呢？看来得回去找回中学历史书看看，到底什么日子会令一群大老爷们对一朵鲜花如此恐惧呢？这些问题是永恒的迷！<br />
