<br />
　　据日本2CH网站15日报道称，韩国最大电视台KBS在6月12日播出的名为“武藏野的开拓者―――若光和1799个高句丽人”的历史节目中称，古代高句丽国最后一个国王的儿子若光于公元666年来到日本，定居日本斡裣亍Ｈ艄庠668年得知高句丽灭亡以后，就将散居在日本武藏国（今日本东京附近）一带的1799名高句丽人集合起来，建立了“高句丽郡”。这1799人均是擅长编织、冶铁等技术的人，他们制作的铁制农具使得日本的农作物收成大大增加。不仅如此，他们还将造纸术传到了日本。他们以日本关东地区为中心，形成了武士集团，这也是后来日本武士集团的“开端”。现在日本关东地区被挖掘的古坟中经常出土铁制武器和农具就是最好的例证，并且日本关东地区是骏马的产地，这也是因为当年定居在关东的高句丽人饲养骏马的原因。 <br />
<br />
　　日本2CH网站还称，韩国的《釜山日报》、Naver网站等各大媒体都转载了KBS的这一“历史考古成就”。报道一出，立即引起日本人的强烈谴责。日本网民纷纷在网上留言“希望韩国尽早停止捏造历史的行为”。日本2CH网站因为不能全部刊登网民的留言，为此还特别在留言页面最上面发表致歉声明。<br />
<br />
