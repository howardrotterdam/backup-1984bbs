　　最喜欢的书：哈里波特<br />
<br />
　　洪欣格说，她非常喜欢读《哈里波特》，最喜欢的人物是“邓布利多”。<br />
<br />
　　最喜欢的作家：张爱玲<br />
<br />
　　洪欣格说，她最喜欢的作家是张爱玲，“张爱玲的文字有一种独特的力量，能抓住人心。”<br />
<br />
　　本报讯 昨日，江苏省天一中学召开新闻发布会，高三（1）班14岁女孩儿洪欣格成为无锡市“中学校长实名推荐”候选人唯一人选。<br />
<br />
　　校长：作文篇篇是满分<br />
<br />
　　天一中学校长沈茂德说：“洪欣格是一个非常阳光的女孩儿，对学习充满了兴趣，自主学习能力非常强。”沈茂德说，他推荐的同学需要具备品学兼优、个性特长、发展潜能三大条件。<br />
<br />
　　据了解，洪欣格英语和语文的学习非常突出，阅读面非常广泛，具有非凡的语言天赋，迄今她已经通过了雅思、托福、SAT等各种考试。洪欣格特别爱好写作，她创作的各种随笔、小说已有50万字之多。“她的文字非常优美，作文篇篇都是满分。”沈茂德这样形容道。<br />
<br />
　　班主任：自学能力惊人<br />
<br />
　　“她自主学习的能力太让人惊叹了！”洪欣格的班主任王明德老师介绍，“她只要有时间就会看书，对她来讲学习是一件非常快乐的事情。”据了解，在小高考过后，洪欣格主动向王明德申请退出语文、英语的学习改而采取自学方式，为此学校还专门为她准备了一间小教室。<br />
<br />
　　王明德透露，其实班里有好多像洪欣格一样优秀的学生，最后12人的推荐小组之所以选择了洪欣格，是她“自主学习能力”非常突出，这一点对于中学教学、中学生自身的学习乃至大学生自身的学习都是一种引导。<br />
