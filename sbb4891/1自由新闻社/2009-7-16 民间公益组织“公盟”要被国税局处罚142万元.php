													<h2>民间公益组织“公盟”要被国税局处罚142万元</h2>
						
						
																											<div id="postmessage_164261" class="t_msgfont">苍天在上――公盟要被处罚142万多元&nbsp;&nbsp;(2009-07-16 10:04:50)<br />
<br />
2009年7月14日，公盟接到北京市国家税务局和地方税务局同时送来的《税务行政处罚事项告知书》，拟于7月24日之前做出行政处罚，地税拟处罚30多万元，国税拟追缴18万多元所得税并处93万多元罚款，两项加起来一共是142万多元。<br />
<br />
其中，地税的30万元处罚是针对耶鲁大学法学院自2006年以来的四笔资助以及公盟研究员王功权先生于2009年4月的资助，耶鲁大学的四笔资助我们于2009年5月已经交了税，功权的资助刚刚报给会计还没有来得及上税就被查了。国税的处罚同样是针对耶鲁大学法学院的合作项目，我们努力解释过，2007到2008年度的合作合同直到2009年初才确认完成，2008到2009年度的合作项目还没有完成，给我们的钱只能算是预付款，而且，公盟得到的捐助全部用于法律研究和弱势群体的法律援助等事项，没有任何剩余，哪里来18万多元的所得税并处93万多元的罚款？<br />
<br />
公盟是一家公益组织，无奈注册成公司，我们一直在申请民政注册。我们认识到了管理确实不够规范，我们都不太懂工商税务，尽管我一再强调过我们的税务不能出现法律问题，但我们聘请的专业会计没有及时帮助我们。在税务稽查的时候，我们默默尽力配合，主动更正一些过错。但是，在丑陋的敌意面前，所有这一切努力都已经没有意义。我们已经缴纳的营业税还是被处罚，我们没有任何盈余还是被处罚所得税，而且，根据法律处罚额度在50%到5倍之间，税务部门没有任何理由一律按最重的5倍处罚。<br />
<br />
142万的处罚，也许对很多企业而言都算不得什么，可是对于公盟而言，这是残忍而邪恶的，这不是对公盟的处罚，这是对毒奶粉受害的孩子、打工子弟学校的孩子、遭遇物业公司欺负的业主、为内心正义奔走呼号的上访者......是对千千万万最需要帮助的无权无势者的处罚，这处罚丧尽天良！<br />
<br />
我们已经很谨慎了，考虑到一些没有良心的畜生们说三道四，我们拒绝接受一些基金会资助，我们选择了耶鲁大学法学院是因为他们也给一些政府部门资助，我了解他们，他们爱中国。公盟与耶鲁大学法学院的合作项目包括北京户籍制度改革研究，提出新移民准入制度的建议；就钉子户问题、西丰县委书记进京抓记者等问题召开研讨会；为小区业主维权呼吁；反对打工子弟学校强制拆迁；为河北承德五次被判处死刑的无辜公民、为被警方打死的杜学磊等一些列极端的冤案提供法律援助；提出司法改革建议；两会期间提出给人大代表的建议，等等。我们所有的努力都是理性的建设性的，我们怀着善良的愿望推动民主法治公平正义，我们从来都是这样怀着纯真的愿望。<br />
<br />
合作项目的钱已经用完了。公盟是一个志愿者团队，我们所有的成员是凭内心的良知和正义感做事，除了很少的办公室专职人员外，我们大部分成员不拿工资，我们把所有的钱都用于良心和正义的事业，我们没有任何利润，从来也没有打算获得利润，我们的收获只是感动。现在如果税务部门要公盟补18万多元的所得税并作出93万多元的处罚，这巨额的罚款只能来自公盟现有的钱――素不相识的朋友们那100元、200元甚至5元、10元的捐助。<br />
<br />
这是不可能的！作为公盟的法定代表人，我宁愿接受七年的刑事处罚，也绝不会把这些5元、10元的捐助交给丧尽天良的畜生们。说我许志永犯了偷税罪，就像临沂的警察指控我是小偷一样，可笑！<br />
<br />
也许有人在背后恶狠狠地说，公盟有政治目的，我对这种说法报以同情。我们的政治目的很清楚，是为了这个国家民主法治公平正义，为了每一个人的自由和幸福，不仅为了我们具体帮助的个体，更是为了建立民主法治健全的制度，让所有的人，包括那些至今仍然对我们怀有敌意的同胞也能获得正义、自由和尊严。<br />
<br />
有人说，公盟就是为社会制造麻烦。我仿佛看到了一张被仇恨和猥琐弄脏了的脸，说，终于逮到了，罚，给我狠狠地罚，只要沾一点边的都给我罚，让你们再给我制造麻烦！不是我们制造麻烦，一年数万起群体性事件不是我们制造的，杨佳不是我们制造的，相反，我们努力把贪官污吏制造的矛盾纳入法治轨道，我们倡导绝对的非暴力，希望我们的社会没完没了的仇恨和冲突能够用爱化解。我们不仅是为了那些遭遇邪恶不公正的受难者，也是为了高高在上的肉食者，我们对这个民族怀有深深的责任――不要让这个国家再出现动荡以至于权贵们死无葬身之地，不要让我们民族的悲剧重演。<br />
<br />
为什么，为什么我们要遭到如此报应？因为我们一身浩然正气，因为我们倡导美好的政治，因为我们的愿望太美好了，因为我们对这个民族从没有放弃希望，因为无论遭遇什么我们内心从来充满希望的阳光。<br />
<br />
我很荣幸再次成为小偷。第一次是在临沂，我被指控为小偷并被带到派出所，在没有辩护人的情况下我的朋友――一个从小双目失明为当地村民的尊严而奔走呼号的中国人以故意毁坏财物罪判处四年徒刑。这次是在北京，我偷税，哈――哈――哈――！我是一个穷人，穷的只剩下信仰了。大人，把我的美好的信仰贡献给您一点行吗？你们应该需要这种信仰，你们应该有能力像我一样心怀慈悲，慈悲地看着鬼魅魍魉们不安的灵魂。<br />
<br />
我是一个穷人，我们是一群穷人，你们不能从我们这里敲诈钱财，也夺不去我们执着的信念。我们没有愤怒，更没有仇恨，我们满怀慈悲，继续走自己的路。公盟不会灭亡的，这个民族的良心和正义的希望不会灭亡的。<br />
<br />
