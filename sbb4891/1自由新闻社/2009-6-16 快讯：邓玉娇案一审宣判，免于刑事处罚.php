													<h2>邓玉娇案一审判决免除处罚</h2>
						
						
																											<div id="postmessage_123734" class="t_msgfont">本文来源于《财经网》　 2009年06月16日 11:18&nbsp;&nbsp;共有 0 条点评<br />
<br />
　　【《财经网》巴东专稿/记者 王和岩】6月16日上午11时，备受瞩目的“邓玉娇刺死官员案”在湖北巴东县法院一审结束。合议庭当庭宣判，邓玉娇的行为构成故意伤害罪，但属于防卫过当，且邓玉娇属于限制刑事责任能力，又有自首情节，所以对其免除处罚。邓玉娇在法律上由此彻底恢复自由身。<br />
<br />
<br />
　　据旁听人员介绍，庭审于上午8时30分在巴东县法院第一法庭开始进行。邓玉娇头扎马尾辫，身着白色T恤、深灰色七分裤，出现在被告席上。她身体略显虚弱，但精神状态还算良好。整个庭审期间，邓玉娇说话不多，声音也比较小，但思路很清晰。<br />
<br />
　　根据出庭公诉的巴东县检察院检察员许雪梅、杨玉莲宣读的起诉书，2009年5月10日晚，巴东县野三关镇招商办主任邓贵大、副主任黄德智等人，酒后到巴东县野三关镇“雄风宾馆梦幻城”玩乐。邓、黄等人欲去水疗区做“异性洗浴”。黄德智发现VIP5包房内正在洗衣的邓玉娇后，进入房间，向邓玉娇提出陪其洗浴的要求。邓玉娇称自己不是水疗区的服务员，并摆脱了黄的拉扯，拒绝了其要求。<br />
<br />
　　随后，邓玉娇离开VIP5包房。在走廊上，邓玉娇遇见了KTV区的服务员唐某，向唐讲客人将她误认为是水疗区的服务员之事，并与唐某一同进入服务员休息室。此时，休息室有罗某某、王某、袁某等三名服务员正在看电视。<br />
<br />
　　黄德智紧跟邓玉娇进入休息室，对其进行辱骂。邓贵大闻声赶到休息室，得知邓玉娇拒绝为黄德智提供“陪浴”服务，便与黄德智一起对邓玉娇进行辱骂，拿出一叠钱炫耀并朝邓玉娇面部、肩部击。<br />
<br />
　　邓玉娇称有钱也不陪浴。经服务员罗某某劝解，邓玉娇欲离开休息室，被邓贵大拉回。此时，闻讯赶来的领班阮某某，对邓贵大、黄德智解释邓不是水疗区服务员，并让邓玉娇离开休息室。<br />
<br />
　　邓玉娇欲再次离开，邓贵大又将其拉回并推倒在沙发上。邓玉娇站起来从随身斜跨的包中掏出一把水果刀藏于背后。邓贵大再次用力将邓玉娇推倒在沙发上。邓玉娇双脚朝邓贵大乱蹬，把邓贵大蹬开，并站起来。邓贵大再次扑向邓玉娇，邓玉娇持水果刀朝邓贵大刺击，致邓贵大的左颈部、左小臂、右胸部、右肩部四处受伤。黄德智上前阻拦，亦被邓玉娇刺伤右臂。邓贵大因伤势严重，经抢救无效死亡。<br />
<br />
　　经巴东县公安局刑事科学技术室法医学鉴定：邓贵大系他人用锐器致颈部大血管断裂、右肺破裂致急性失血性休克死亡。黄德智右臂为轻伤。<br />
<br />
　　公诉人同时称，案发后邓玉娇主动向公安机关投案，并如实供述案件事实。<br />
<br />
　　起诉书载明：侦查期间，受公安机关委托，湖北省人民医院法医精神病司法鉴定所和武汉市精神病医院司法鉴定所，对邓玉娇进行了精神病医学鉴定，结论为：“邓玉娇为心境障碍（双相），属部分（限定）刑事责任能力。”<br />
<br />
　　公诉人认为，邓玉娇在制止邓贵大、黄德智正在进行的不法侵害过程中，致一人死亡，其防卫行为明显超过必要限度，属于防卫过当。依照《刑法》第21条、第234条的规定，犯罪事实清楚，证据确实充分，应当以故意伤害罪追究其刑事责任。但应当减轻或者免除处罚。同时，鉴于邓玉娇属尚未完全丧失辨认或者控制自己行为能力的精神病人，依照《刑法》第18条，可以从轻或者减轻处罚。其具有主动投案自首的情节，也可以从轻或者减轻处罚。<br />
<br />
　　据旁听庭审的有关人士介绍，邓玉娇的辩护律师汪少鹏、刘钢为邓玉娇做了无罪辩护。他们强调，邓玉娇的防卫行为是适当的、适度的，并没有超过必要限度；邓玉娇的行为同时也符合《刑法》第20条的规定，具有无限防卫权，不应负刑事责任。<br />
<br />
　　经过近一个小时的法庭辩论后，法官于上午10时30分宣布休庭。经过合议庭合议后，法官于上午11时宣布了判决结果：邓玉娇的行为构成故意伤害罪，但属于防卫过当，且邓玉娇属于限制刑事责任能力，又有自首情节，所以对其免除处罚。<br />
<br />
　　该判决对检方的指控基本认可，对律师的辩护意见也部分予以采纳。<br />
<br />
　　据《财经》记者了解，巴东县法院一号法庭能容纳50余名旁听人员，但庭审仅有新华社、人民日报、湖北卫视、恩施电视台、长江巴东网等八家媒体被允许旁听。对其他媒体的旁听申请，巴东县有关当局以旁听证已经发放完毕为由婉拒。<br />
<br />
　　从早晨7时许，巴东县法院门口就开始有人群聚集。至庭审结束，大约有四五百人在法院外围观。巴东县有关部门特意在法院门口及街道两旁，布置了警力维持秩序。<br />
<br />
　　判决后，维持秩序的警察开始劝说大家散去。至《财经网》发稿时，巴东县法院门口仍有群众聚集，但秩序良好。■ <br />
