																											<div id="postmessage_123741" class="t_msgfont">免除处罚　　又称“免予刑事处分”。对犯罪分子作有罪宣告，但免除其刑罚处罚。免除处罚以行为已构成犯罪应受刑罚处罚为前提。<br />
　　它与中国刑法第十条中规定的“情节显著轻微危害不大的，不认为是犯罪”以及“无罪释放”截然不同。前者是有罪而免罚；后者是不构成犯罪，不应当受到刑罚处罚。免除处罚指免除刑罚处罚，而不是免除一切处分。<br />
　　①刑罚上的免除处罚，即“免予刑事处分”，指对于犯罪情节轻微或有某种特殊情况(如犯罪后自首且有重大立功表现)的犯罪分子不判处刑罚。其前提是确认犯罪分子有罪，因此虽不判处刑罚，仍可给予其他处分。我国刑法规定了免除处罚的情节。②违反治安管理的处罚也有免除处罚，我国治安管理处罚条例对其情节作了规定。<br />
　　法定“应当”免除处罚的情节有：(1 )防卫过当；(2)避险过当；(3)中止犯；(4)从犯；(5)胁从犯。<br />
　　法定“可以”免除处罚的情节有：(1)在中国领域外犯罪，但是在外国已经受过刑罚处罚的， (2)又聋又哑的人或者盲人犯罪的；(3)预备犯；(4)犯罪后自首的；(5)个人贪污数额在2000元以上不满5000元；犯罪后自首、立功或者有悔改表现、积极退赃的；(6)行贿人在被追诉前；主动交待行贿行为的。<br />
　　中国刑法中通常将免除处罚与减轻处罚，或者与从轻、减轻处罚并列规定在一个条文中，供量刑时选择适用。刑法理论上，免除处罚有“法定免除”和“酌定免除”之分。<br />
