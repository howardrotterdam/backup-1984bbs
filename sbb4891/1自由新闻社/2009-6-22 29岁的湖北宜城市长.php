　　<br />
　　　　范宇知道妻子处在那样重要的岗位上，必须以最饱满的精神状态投入工作，哪怕一点点烦恼都会影响她的工作状态。因此，他在生活里更加宽容地对待妻子，李修平遇到烦心事向他发牢骚，他从不与她顶撞总是耐心地安慰她。平时在家里，经常放一些舒缓的音乐驱散她工作的疲惫。有时，李修平主动和他聊起工作上的事情，他总是想方设法扯开话题。他告诉妻子：“家是休息的地方，不要把工作带到家里来。” <br />
　　<br />
　　　　每年春节，李修平都要和丈夫回老家陪父母过年，父亲从小就对李修平教育很严格，在她去上大学时，父亲曾送给她几句话：“生活要朴素，学习要努力，做人要坦诚。”这成了李修平的座右铭，2003年父亲病倒在床，因为李修平工作很忙，范宇就抽时间回兰州照顾岳父，替妻子尽女儿的责任和孝心。 <br />
　　<br />
　　　　不久，父亲还是走了，李修平悲痛万分，工作都受到了影响。范宇就开导她说：“爸爸已经走了，再悲伤也没有用，爸爸如果知道你这个样子，他会难过的。爸爸生前最感到自豪的，就是自己的孩子都很优秀，你只有把最好的状态放在工作上，做得更出色，才是对爸爸最好的怀念啊。” <br />
　　<br />
　　　　是啊，丈夫说的对，李修平意识到自己唯一能做的，就是好好工作，报答父亲的在天之灵。 <br />
　　<br />
　　　　父亲走了，年迈的母亲独自在兰州生活，对母亲的牵挂常常令李修平忍不住落泪。丈夫明白了李修平的心事，认真地对她说：“妈妈年纪大了，需要人照顾，我们把她接到北京来一起生活吧。”丈夫的理解让李修平非常感动，夫妇俩一起回到兰州，把母亲接了过来，与他们生活在一起。范宇对待岳母像亲生母亲一样，一家人生活得温馨和睦。 <br />
　　<br />
　　　　成名这么多年来，李修平一直保持着一颗难得的平常心。面对种种物质的诱惑，她不为所动，丈夫很欣赏她这种心素如简、淡定如菊的品德。 <br />
　　<br />
　　　　在许多人的眼里，李修平是“国脸”，而她的丈夫是普通人，他们之间的差距很大，李修平却不这样认为。在她眼里，丈夫非常优秀，很有男子汉气概，是她的主心骨。在家里，她非常乐意做个小女人，家里的大小事情，她都喜欢丈夫拿主意。李修平非常尊重丈夫，丈夫不愿意在媒体上露面，她从来不强求他。一转眼，“国脸”李修平与平凡丈夫的婚姻已经走过了十多个年头。他们从容淡定的生活态度是对“平平淡淡才是真”的最好诠释。 <br />
　　<br />
　　　　2006年2月28日，在接受笔者采访时，李修平的脸上浮现着菊花般淡雅的微笑：“名利不过是过眼烟云，对我来说，有一个温暖和睦的家，有一个相濡以沫的丈夫，是我这辈子最大的财富。”</div>

							
							
							
							
