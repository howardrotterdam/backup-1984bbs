&nbsp; &nbsp; 对于周森锋来说，24岁才研究生毕业参加工作，但工作之后就成为副处级干部（由襄樊向清华大学引进高素质人才），担任了襄樊市建设委员会副主任，不久又出任规划局党组副书记、副局长，2008年赴宜城任职，担任常务副市长，目前已经拥有3个副处级岗位，担任副处级已经5年，今年成为正处级干部，完全符合《党政领导干部选拔任命工作条例》，也不会产生像张辉、孙靓靓等年轻干部一样的非议。当然，要寄望于产生很多的29岁县长不太现实，但根据《年轻干部晋升指南1000问》的许多案例看，35岁将成为年轻干部晋升的分水岭，因为不光政策规定，县市至少要选拔任命产生2名以上年龄相当的党政领导干部，特别是今年上半年召开的培养选拔年轻干部座谈会上，据透露全国将重点培养万名以上35岁左右的处级干部，其中将有相当大的比例将进入县市正职。<br />
<br />
二、高学历<br />
周森锋2004年从清华大学研究生毕业，拥有了从政的好起点。近年来，地方组织部门与各大名校高校加大了引进优秀人才的措施，因此，对于拥有名校背景或高学历的年轻公务员来说，无疑是个利好。《年轻干部晋升指南1000问》中分析的近100名年轻干部成长来看，选拔使用年轻干部对于学历和知识面的要求越来越高。特别是新世纪超常规提拔产生的知名年轻干部，近半数是高学历官员。<br />
三、基层锻炼<br />
从周森锋的经历上看，他与其他成名的年轻干部相比，最大的一个特点就是不是从团干成长起来的。还是一毕业就在建设和规划部门，特别是去年更是担任工作任务比较压头的常务副市长，从江汉传媒网和宜城市政府公众信息网对周森锋相关的报道中可以看出，周森锋从政务实、低调，在城建工作中许多。事实上，中央培养使用年轻干部的政策已经有所微调，团干可能将不再成为主渠道，拥有丰富的基层工作经历、经济管理经历者将更加被得到重用。<br />
四、机遇<br />
对于周森锋来说，能够在短时间内成为县市党政正职，拥有三个机会，一是利用襄樊两年内引进硕士博士182人的机会，从清华赴襄樊，并一开始就安排为副处级干部；二是工作4年后就被组织安排到县市基层工作；三是去时时机比较好，当时襄樊官场大地震，影响了大批干部，在这种背景下，周森锋作为一个外来干部和年轻干部，再加上自己的政绩，仕途顺利也就顺理成章了<br />
