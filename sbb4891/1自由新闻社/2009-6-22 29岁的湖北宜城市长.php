　　　　这次相识后，范宇开始向李修平发起了爱情攻势。终于有男孩子敢追自己了，李修平感到一丝欣慰。在外人看来，作为名人的李修平应该找一个两相匹配的精英级男孩做男朋友，而范宇只是一个普通工程师，无法与李修平对等。这样的议论清晰地传到了范宇的耳朵里，他不仅没有退缩，反而坦然地对李修平说：“在电视圈，你是精英；在建筑领域里，我也是非常优秀的。别人之所以认为我与你不匹配，其实是职业特征造成的。” <br />
　　<br />
　　　　范宇的勇气、自信和坦荡，最为李修平所欣赏。男人就应该刚毅，豪情万丈，一个连他喜欢的女孩都不敢追的男人，又怎么会有勇气去挑战、去迎接生活中的风风雨雨？ <br />
