　　<br />
　　　　1992年，29岁的李修平与30岁的男友终于举行了婚礼。 <br />
　　<br />
　　　　“牛郎织女”终于团聚后反而冲突不断 <br />
　　<br />
　　　　婚后，李修平与丈夫分居两地，他们像牛郎织女一样在北京与兰州守候彼此那份难得的感情。因为夫妇俩的工作都很忙，短暂的相聚对他们来说显得尤为珍贵。每逢大的节假日，丈夫千里迢迢来北京看望李修平。可团聚的美好时光总是那么短暂，住不了几天， <br />
　　<br />
　　　　丈夫就要回到兰州。为了夫妻团聚，李修平一直在想办法把丈夫调到北京来，但要进京谈何容易？虽然他们费尽周折，却依然进展缓慢。这样的日子一过就是3年。3年后，范宇终于调到了北京，进入一家国有建筑公司工作。夫妇俩终于团聚了，李修平有一种苦尽甘来的欣慰。可是，快乐还没有享受多久，问题很快就来了：在此之前，李修平与丈夫相处的时间最长都没有超过一个月，所以都尽量让着对方；可现在每天朝夕相处，情况就变了。要知道，多年的独处生活，让李修平和丈夫养成了独立的生活习惯，现在对方的介入，将彼此生活的平衡打破了，他们在心理上反而难以适应。李修平喜欢安静，没事的时候爱在家里看书，而范宇喜欢热闹和应酬，每每出去的时候还要拉上李修平；李修平喜欢看话剧，听交响乐，而丈夫则青睐好莱坞大片。 <br />
　　<br />
　　　　平时在家里，两人也找不到共同的话题，这让李修平很痛苦，两人常常为一些琐事发生摩擦。团聚后反而冲突不断，这是两个人都没有想到的。夫妻之间的矛盾，令李修平感到沮丧、痛苦，心情最糟糕的时候，她甚至想到要离婚……就在李修平对婚姻失望透顶的时候，一个偶然，却让她茅塞顿开。那天，李修平应邀去《夕阳红》栏目做嘉宾主持，主题是一些金婚老人谈如何经营婚姻，看到这些白发苍苍的老年夫妇一对对恩爱无比，从骨子里渗透出来的那份默契，令李修平忍不住热泪盈眶，她忍不住问他们有什么相处的秘诀，一对年届九旬的夫妇侃侃而谈：“世上没有两片完全相同的树叶，生活中没有不吵不闹的夫妻，婚姻需要包容，需要坚持，学会以一颗感恩的心去对待对方……”一语惊醒梦中人！李修平恍然大悟：是啊，要尽量看到对方身上的优点，不要 <br />
