在我眼里，许志永就是一卖国贼（转帖）&nbsp;&nbsp;<br />
<br />
2008．8．4<br />
<br />
&nbsp; &nbsp; 最近许*志*永事件被炒的很热，咱做为天天流连海外华人网站只为关注新疆事件（国内网络太和谐了，除了分裂分子，民族团结，没别的内容）的普通人，有话要说。<br />
&nbsp; &nbsp;我是从邓玉娇案先知道公*盟这个组织的，然后知道有许*志*永。在然后，从《纽约时报》一篇写新疆事件的文章（题外话说句，该文捏造事实歪曲真相反动透顶）里看到说公*盟被查了。 然后咱关注了一下，就知道的越来越多了。<br />
&nbsp; &nbsp;说税收问题，税收上出问题的人和企业多了，人家该交税交税该处罚处罚，刘晓庆毛阿敏在税收问题上被暴光被处罚，税交了款罚了，仍然要按税法刑法坐牢，和他们比，许*志*永偷税难道就是应该的吗？不要说那款项是捐款，在税收上企业如果有不明款项进入银行户头，没开票且不入帐，就是有偷税嫌疑，许说税收都交给会计的，他不知道，屁话，作为一个律师的，不知道遵守法律吗？纳税申报表难道许先生你没签字吗？<br />
&nbsp; &nbsp;一个人因为过去有功劳，现在他违法犯罪了，是否就可以逃避处罚呢？<br />
&nbsp; &nbsp;税收上被查，觉得税务部门处理有问题，可以申请听政，可以打官司，炒作什么呢，许在为他税收问题申辨中写道：“142万的处罚，也许对很多企业而言都算不得什么，可是对于公*盟而言，这是残忍而邪恶的，这不是对公*盟的处罚，这是对毒奶粉受害的孩子、打工子弟学校的孩子、遭遇物业公司欺负的业主、为内心正义奔走呼号的上访者……是对千千万万最需要帮助的无权无势者的处罚，这处罚丧尽天良！”<br />
&nbsp; &nbsp; 自己涉嫌偷税，却说税收执法行为 “残忍而邪恶”，把公司违法所受的处罚和“毒奶粉受害的孩子、打工子弟以学校的孩子……”联系起来，哈哈哈，刘晓庆怎么没说税务部门，你们居然让我坐牢，真是丧尽天良，许先生，你作为一个律师，税收问题却不想通过法律方式来解决，却想以舆论方式来施压，甚至想把税收处罚政治化，实在是很不厚道，对于律师来说，更是个笑话。做为律师，如果法律方式不能解决问题，你还可以申请律师协会关注，为什么正当的渠道你不先走，处罚通知书一收到，你就先忙着舆论炒作，咱就不明白了。<br />
&nbsp; &nbsp;“丧尽天良”这四个字，除了让我吃惊，还似曾相识，这个词似乎还在另外一个地方被使用过，想起来了，是邓玉娇案，公*盟律师夏林也同样说过这么一句话。咱不是律师，不评价夏林，咱个律师朋友认为，夏林去之前，从前方先出的屠夫那，已知道邓可能被性侵犯，为避开警方视线，应该全盘考虑，先取证，之后再见邓，作为多年的刑事律师，这样的纰漏都能出，我的朋友觉的他很失水准。之后见邓的表现我的律师朋友更加觉得不可理解，见邓出来的两次，在着媒体，第一次见面出来高兴地打v字手势，透露刀子是邓自己买的，第2次见面出来和另一律师抱头大哭着说“他们灭绝人性，丧尽天良”，让人怎么相信这是一个当律师十几二十年的水准呢，难道他们不知道警方也在看着他们吗？<br />
我的律师朋友对他们这样的喜怒表现于色的表现有个推测：<br />
1， 比较善良的推测，面对媒体，太激动了，不知道怎么应对了；<br />
2，比较恶毒的推测，出名的愿望太强烈，超过了应有的职业道德；<br />
俗话说，不怕不识货，就怕货比货，对比一下同期轰动全国的杭州飚车案中谭卓一方的律师，你们就知道差距在哪里了。<br />
&nbsp; &nbsp; 谭的律师我都现在是啥名字啥长相都没记住，可是我记得他在谭卓一方拿到赔偿后第2天（6月23日）接受一个某周刊的采访，他对谭家的保护的表现我看的很清楚，说到赔款，他说：谭父没有提赔偿数目，只是说钱不能买刑，于是我帮助他计算了一下..........<br />
&nbsp; &nbsp; 你们真相信谭家人没为赔偿问题和金额和律师做过很明白的沟通么？律师对媒体的说法其实是为了保护谭家在公众在胡家在警方面前的名誉，整个赔偿问题，都是他冲锋在前地谈判，只花2天时间，他在没给胡家任何减刑承诺的基础上，为谭家拿到比法律条文规定的最高赔偿数还多出40万的赔偿额，且保护了谭家在公众面前的名誉，并安排谭家在拿到赔偿第2天就离开杭州了，做出这样的成绩，律师功劳不可谓不大吧。象谭家这样的律师，才是真正值得信任的，他冲锋在前，享受在后，个人能借案件出名当然好，但委托人利益才是第一位的。作为轰动一时的案件的免费代理律师，案子办下来公众有几个人能了解他在媒体背后所做的努力，有几个人能记得他的名字记得他的长相，对比一下吧。<br />
