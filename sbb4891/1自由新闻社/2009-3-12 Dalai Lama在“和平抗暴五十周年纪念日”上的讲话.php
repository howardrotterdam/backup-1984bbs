从1949&#63886;开始，中共的武装&#63882;&#63870;，从西藏的东&#63843;部和东部（安多和康）渗入。1950&#63886;，超过五千藏军战败后，中国政府现实地选择&#63930;和平解放西藏的策&#63862;，1951&#63886;签订&#63930;“十七条协议”和附加条款，从此，西藏属于中华人民共和国管辖。但是，对保护西藏特殊的宗教、文化和风俗，“协议”里有明确的承&#63837;。1954--55&#63886;，在&#63843;京，我和以毛泽东为首的党政军大部分高层&#63924;导人的接触中，详细地谈到&#63930;在保护西藏珍贵的宗教文化基础上，发展西藏社会和经济的具体方向。毛泽东还根据“协议”内容，在&#63847;必建&#63991;军政委员会，可以成&#63991;自治区筹委会等方面，做&#63930;积极的指点和保证。然而，1956&#63886;左右出现的极左&#63799;线，使那些承&#63837;在地方无法兑现；而且，安多和康区还进&#64008;&#63930;脱&#63978;现实的强制改革，导致&#63930;动荡和破坏；西藏中部的中国官员，也故意蛮横地践踏“十七条协议”等，强压&#64008;为越&#63789;越严重，藏人被逼无&#63756;，最终，于1959&#63886;3月10日举&#64008;&#63930;和平抗&#64006;。当时，仅在几个月的镇压中，就有数万藏人被打死，关押、逮捕。同时，我和噶厦及政府的部分公务员，一起&#63946;亡印&#64001;。十万藏人，也先后被迫&#63946;亡印&#64001;、尼泊尔和&#63847;&#63838;。这非同寻常的灾难，即使今天，藏人仍然记忆犹新。<br />
