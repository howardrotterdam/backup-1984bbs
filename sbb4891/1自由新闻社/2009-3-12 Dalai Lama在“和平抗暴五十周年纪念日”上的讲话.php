达赖喇嘛尊者在西藏3・ 10和平抗暴 五十周年纪念集会上的讲话（全文）<br />
<br />
今天，是藏人因无法忍受中共的强权高压，于1959&#63886;在首府&#63781;萨举&#64008;和平抗&#64006;五十周&#63886;纪&#63907;日。去&#63886;3月10日，参加西藏三区和平抗&#64006;的人们，多数在1959&#63886;以后出生和成长，尽管他们没有亲历自由的西藏，但是，对西藏事业的关怀和忠贞&#63847;渝，是世代延续而&#63789;的，值得自豪，也鼓舞着国际上所有关注西藏问题的人们。 此时，我们要缅怀那些死去和正在遭受酷刑，以及在过去的50&#63886;里，所有为西藏教、政事业献出生命和饱受折磨的藏人，并向三宝特别祈祷。 <br />
