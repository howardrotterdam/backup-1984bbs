<br />
　今年夏天的平壤街头，让人眼前一亮。在人们的印象里，朝鲜人总是穿着单调的黑、白、灰色的衣服，其实那是一种误解，看看今年夏天的平壤街头，着实让人眼花缭乱。除了日常的工作装以外，许多朝鲜年轻女性爱上了朱红色的衬衫、碎花连衣裙、扎染的薄纱套装，还有各种五颜六色的T恤衫。常驻朝鲜的外国人以赞扬的口吻称：这种现象叫“朝鲜女性的新时尚”。<br />
<br />
　　平壤市民尤其是年轻女性的这种变化，引人注目，在同常驻平壤的一些中国人聊到这个话题时，他们也很感兴趣。一位在金日成综合大学留学5年的中国留学生说：“现在朝鲜的年轻姑娘们真的是越来越会打扮了。平壤一些市场上出售的衣服同我刚来的时候相比风格有很大变化，许多衣服式样真的很时尚，我们留学生偶尔也会去‘淘淘宝’ 呢。”<br />
<br />
　　据了解，不光是普通的市场上有如此的变化，在平壤的外汇商店中，体现得更加明显。到外汇商店购买进口服装的平壤市民络绎不绝。这些服装大多是从中国、日本还有欧洲进口的，有许多平壤女性热衷于购买最新款式的服装。在5月份结束的平壤国际商品展览会上，一位中国商家告诉记者，服装同日用品一样是展览会最畅销的商品，拿来多少，卖多少，而且越是颜色鲜艳的越是最先卖完。<br />
<br />
　　不光年轻女性是时尚的追捧者，近期在朝鲜街头，好多年长的妇女们也褪去了传统单一的颜色，穿上了五颜六色的花衣裳。年长的妇女们大多钟爱“三件套”套装，有一些人还穿着半透明的蕾丝开衫。在朝鲜的“时尚圈”中，还有不能忽视的一群人，那就是朝鲜的“海归”们。他们有时穿的甚至比我们这些外国人还新潮。<br />
<br />
　　其实，朝鲜人有他们自己的服装理念。他们认为服装不仅反映了各个国家、各个民族的民族感情和生活风俗，也反映了相关社会的阶级性质，朝鲜人着装的标准是要符合社会主义生活方式，衣服要干净、端庄、朴素和美丽。在朝鲜人的服装文化中，非常重要的一点就是衣服的颜色和样式要和身材相协调，这对提高一个人的品位具有重大意义。<br />
<br />
　　可见，朝鲜人对美的理解是独到的，随着时代的发展，朝鲜人对美的理解也在逐渐发展，而且努力在适应和追赶这种变化。五颜六色的服装给炎炎夏日里的人们带来了些许清凉感，成为平壤市又一道亮丽的风景。<br />
