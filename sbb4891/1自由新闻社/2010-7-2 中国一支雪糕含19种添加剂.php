																											<div id="postmessage_717576" class="t_msgfont">一支雪糕含19种添加剂 你还敢不敢吃？<br />
 2010-07-02 <br />
　　星岛环球网消息：6月初，国家质检总局颁发的《食品添加剂生产监督管理规定》正式实施，其中规定，所有食品添加剂成分，必须在包装上毫无保留地明示。<br />
<br />
　　《潇湘晨报》报道，不看不知道，一看吓一跳，在包装上面发现，区区一支雪糕，竟然含有19种添加剂。<br />
<br />
　　羧甲基纤维素钠、黄原胶、单硬脂酸甘油酯、瓜尔胶、卡拉胶、柠檬酸……这些拗口的专业名词让人颇有些摸不着头脑，究竟这么多的添加剂是否有害、是否有必要、是否被滥用了呢？<br />
<br />
　　“只要按国家标准添加食品添加剂，消费者就可以放心食用。”省质监局的专家表示。不过，他坦言，即使是一款符合国家标准的产品，被一个人毫无节制地食用，那么产品当中的添加剂逐渐累积同样会给人身健康带来隐患。<br />
<br />
主要用于调味、着色、塑形、防腐<br />
<br />
　　某品牌蓝莓酸奶味雪糕包含了34种配料，其中食品添加剂多达19种：磷脂、聚甘油蓖麻醇酯、饴糖、全脂乳粉、果葡糖浆、麦芽糊精、乳酸、乳酸钠、羧甲基纤维素钠、黄原胶、单硬脂酸甘油酯、瓜尔胶、卡拉胶、柠檬酸、柠檬酸钠、食用香精（酸奶香精和蓝莓香精）、甜蜜素、笕菜红、亮蓝。<br />
<br />
　　那么这些添加剂在雪糕中究竟扮演着怎样一种角色呢？<br />
<br />
　　“最近几年添加剂越用越多，跟这两年饮料、包装食品越来越追求新奇口味也不无关系，要创造各种口味，往里面加添加剂就成了最好的解决办法。”湖南省质监局食品质量监督检验所总工程师杨代明表示。<br />
<br />
　　比如食用香精，在食品行业里面，它就被形容为“注重于香气和味觉的仿真性”。<br />
<br />
　　此外，食品添加剂还被用于着色、塑形、防腐等方面。例如，在一支雪糕所含的19种添加剂中，笕菜红、亮蓝就用于着色；卡拉胶、黄原胶、单硬脂酸甘油酯等就用于塑形；乳酸、羧甲基纤维素钠、柠檬酸钠等具有防腐保鲜的作用。<br />
<br />
适量使用添加剂无危害<br />
<br />
　　“目前我国允许使用的食品添加剂共有22类，1812种，在食品生产中只要按国家标准添加食品添加剂，消费者就可以放心食用，合理使用添加剂对人体健康是有益无害的。”杨代明说道。<br />
<br />
　　据其介绍，符合国家安全标准添加的食品添加剂在改善食品的品质，提高食品的质量和保藏性，满足人们对食品风味、色泽、口感的要求同时使食品加工和制造工艺更合理、更卫生、更便捷，有利于食品工业的机械化、自动化和规范化，为食品工业节约资源，降低成本，在极大地提升食品品质和档次的同时，增加其附加值，产生明显的经济效益和社会效益等方面具有很重要的作用。<br />
<br />
　　“这也是国家允许添加的重要原因和依据。”杨代明表示，但是过量添加食品添加剂的商品会给消费者的人身安全带来诸多的隐患，引发一些疾病。<br />
<br />
　　那么，一支雪糕里面19种添加剂究竟算不算多呢？<br />
<br />
　　杨代明认为，实际上添加剂的种类和剂量并没有直接的关系，“添加剂使用规定上有对总量规定的计算公式，所以不一定种类多就代表总量多。比如羧甲基纤维素钠、果胶、结冷胶都是增稠剂，在同一款饮料里配比在一起以后能起到一个协同效应，就像中药配方一样，在起协同效应之后反而能减少总用量。”</div>

							
							
							
							
