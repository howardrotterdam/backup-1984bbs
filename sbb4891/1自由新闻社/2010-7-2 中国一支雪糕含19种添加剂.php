<br />
　　记者：“你这样做西瓜生意多长时间了，是不是把批发来的生西瓜集中到一起再作处理？”<br />
<br />
　　武某：一开始是这样的，刚入行时，对技术掌握不过关，另外还有点害伯，这样赚不了多少钱的！<br />
<br />
　　记者：那你是怎么通过贩水果发家的呢？<br />
<br />
　　武某：那是一个机会，大约两年前，我在山东枣庄进了10吨货，由于贪便宜，进了一车生瓜，当时将货运到了南京，瓜卖不去，心想这次损失可大了。正在发愁时，几个老乡来帮忙，一个晚上，这些西瓜全部都做了“手术”。3天后，切开这些西瓜我都要跳起来了，西瓜个个瓤红味甜，一车瓜很快销售一空，那一趟不但没赔钱，反而狠狠地赚了一大笔。从那以后，我就专门以低价在山东、安徽等地采购价格低的生瓜，加工后上市热销。两年来，我摇身一变成了西瓜大王。&nbsp;&nbsp;<br />
<br />
<br />
手脚从瓜田做起<br />
<br />
　　记者：你这样做有没有被查到过？<br />
<br />
　　武某：没有，一次都没有。贩瓜季节南京对运瓜车辆提供了很多优厚的待遇，再加上在地头采摘时，就开始对瓜动“手术”，等运到南京时，西瓜已经“成熟”了。再说，西瓜又不像其它食品，做了手脚能查出来。就是查也查不了许多，有时为了安全起见，我们也会进少量的好西瓜夹杂在里面以掩人耳目。<br />
<br />
　　记者：所有瓜贩都会这样赚钱吗？<br />
<br />
　　武某：在我认识的瓜贩当中，大部这样做过。不这样做肯定赔本。最赚钱的是西瓜刚刚上市那会儿。为了早上市卖个好价钱，有的直接到瓜农那儿，将整个瓜地包下来，在西瓜还没有摘下来时就开始注射糖精和色素，这样效果会更好。<br />
<br />
　　记者：二道瓜贩知道这种瓜后，他们会进你的货吗？<br />
<br />
　　武某：进！肯定进，一开始他们也有所顾虑，后来由于这种瓜味甜瓤红，买的人多，瓜贩们也熟悉了，这就成了西瓜市场的一个公开秘密了。但是，小贩们卖瓜时绝不会说这是有色素的，再说老百姓只顾好看味甜，谁又想到有这样的手段呢？<br />
<br />
　　说话间一个多小时过去了，武某剖开了一只西瓜，原先的生瓜，已开始变红了。“这些瓜如果摆一天时间，打开后你绝对不会认为这是‘手术’过的西瓜。”<br />
<br />
　　当记者还想再了解一些情况时，武某告诉记者，他今天是凭着良心才自揭内幕，但他也不想给自己带来麻烦，这间地下室就正好到期，下午他也离开南京了，改行做其他生意。<br />
<br />
<br />
什么是有毒西瓜<br />
<br />
西瓜果肉水分较多，且味甜，是人们夏季消津止渴的佳品。但是购买西瓜不慎，也可导致人体中毒。通常，人们称这种西瓜为毒西瓜。<br />
<br />
　　毒西瓜的毒性来源于瓜农施用过量的激素(催熟剂、膨大剂)和农药。人们吃了这种西瓜后，会出现恶心、呕吐、腹泻等中毒症状。所以，人们选购西瓜时要注意识别毒西瓜。<br />
<br />
　　识别毒西瓜要从以下几点加以注意：<br />
<br />
　　1、瓜皮上的黄绿条纹不均匀，切开后瓜瓤特别鲜艳，但瓜子却是白色的，吃起来没有甜味。<br />
<br />
　　2、施用了膨大剂的西瓜个儿大，一般可达6～10公斤。一般重量在4公斤左右西瓜是正常的。<br />
<br />
　　3、施用过激素的西瓜，由于喷洒农药和吸收不均匀，易出现歪瓜畸果，如两头不对称、中间凹陷、头尾膨大等，表面有色斑或色差大，这种歪瓜畸果不要买。正常的西瓜的外形应是球形或椭圆形的，且表面平整光滑。<br />
<br />
　　4、人们食用西瓜时，若发现口感不好，尤其是舌头有麻感时，应立即停止食用，不可心存侥幸。</blockquote></div>ps:我卖糕的和上海帅哥是一个人吗，<br />
