毛主席曾三番五次对日本访华政要表示感谢，这本是一件众所周知的事情。这些文献皆来自大陆正式出版物。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;1、日本皇军是大恩人，大救星！<br />
　　1964年7月10日，日本社会党委员长佐佐木更三偕委员黑田寿男来北京，与毛有下面一段对话∶<br />
　　毛∶“我曾经跟日本朋友谈过。他们说，很对不起，日本皇军侵略了中国。我说∶不！没有你们皇军侵略大半个中国，中国人民就不能团结起来对付蒋介石，中国共产党就夺取不了政权。所以，日本皇军是我们中国共产党人的好教员，也可以说是大恩人，大救星。”<br />
　　佐佐木∶“今天听了毛主席非常宽宏大量的说话。过去，日本军国主义侵略中国，给你们带来了很大的损害，我们大家感到很抱歉。”<br />
　　毛∶“没有什么抱歉。日本军国主义给中国带来了很大的利益，使中国人民夺取了政权。没有你们的皇军，我们不可能夺取政权。这一点，我和你们有不同的意见，我们两个人有矛盾。”（众笑，会场活跃）。<br />
　　佐佐木∶“谢谢。”<br />
　　毛∶“不要讲过去那一套了。日本的侵略也可以说是好事，帮了我们的大忙。请看，中国人民夺取了政权，同时，你们的垄断资本、军国主义也帮了我们的忙。日本人民成百万、成千万地醒觉起来。包括在中国打仗的一部份将军，他们现在变成我们的朋友了。”（摘自《毛泽东思想万岁》，第533至534页。）<br />
<br />
<br />
　　2、你们有功呀，我们不要你们赔偿！<br />
　　1972年中日建交，日本首相田中角荣向毛道歉：“啊，对不起啊，我们发动了侵略战争，使中国受到很大的伤害。”<br />
　　毛：“不要对不起啊，你们有功啊，为啥有功呢？因为你们要不是发动侵华战争的话，我们共产党怎么能够强大？我们怎么能够夺权哪？怎么能够把蒋介石打败呀？”他感谢田中角荣。“我们如何感谢你们？我们不要你们战争赔偿！”（翻译摘自《田中角荣传》日语原版）<br />
<br />
<br />
　　3、原子弹损害了日本人民，美国名声不好！<br />
　　毛：“美国在日本的广岛和长崎投了两个原子弹，损害了日本人民。因此使美国在世界大部分人民中间的名声不好。……第二次世界大战中，日本政府是强迫日本人民进行侵略战争的，但后来起了变化，遭到了美国的原子弹之害，所以日本人民，包括某些政府人士也反对战争。”（同参加在日本召开的第十一届禁止原子弹氢弹世界大会后访华的外宾谈话1964年8月22日）<br />
　　　　<br />
　　4、已经赔过不是了，不能天天赔不是！<br />
　　毛：“第二次世界大战以后，把我们的关系改变了，刚才已经讲到，你们是处于很好的地位，处于理直气壮的地位。过去你们欠过人家的帐，现在你们不再欠帐了，而是有人欠你们的帐。你们现在很有政治资本，我们也有政治资本，向美国讨帐。它欠了我们的帐，这一点，我想我是根本没有讲错的。你们现在是轻松愉快了，与第二次世界大战时不同了，理由抓在你们手里，是不是？对不对？对你们过去欠的帐再要来讨帐，这是没有道理的。你们已经赔过不是了。不能天天赔不是，是不是？一个民族成天呕气是不好的，这一点，我们很可以谅解。我们是你们的朋友，你们对中国人民看得清楚，不是把你们当作敌人看待，而是当作朋友看待的。（同日本国会议员访华团谈话1955年10月15日）<br />
　　<br />
　　　<br />
　　5、感谢日本皇军侵略中国！<br />
　　毛：“日本皇军过去占领了大半个中国，因此中国人民接受了教育。如果没有日本的侵略，我们现在还在山里，就不能到北京看京剧了。正是因为日本皇军占领了大半个中国，让我们建立了许多抗日根据地，为以后的解放战争创造了胜利的条件。日本垄断资本和军阀给我们做了件‘好事’，如果需要感谢的话，我倒想感谢日本皇军侵略中国。”(毛会见日本社会党议员黑田寿男等人时的谈话 1961年1月24日)<br />
