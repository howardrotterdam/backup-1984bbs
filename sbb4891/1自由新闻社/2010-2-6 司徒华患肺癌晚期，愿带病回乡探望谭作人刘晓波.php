																											<div id="postmessage_470428" class="t_msgfont">2010年02月07日 <br />
<br />
【O果日报讯】司徒华将於年宵推出新书<strong>《栏干拍遍》</strong>，结集其报章专栏散文。新书的前言写於上月 9日，字里行间满有感触。<br />
<br />
<strong>深信壮志有人去实现</strong><br />
<br />
据商台《左右大局》讲述，华叔前日在家为《栏干拍遍》签名。书名取自南宋词人辛弃疾作品《水龙吟》（登建康赏心亭）其中一句：「落日楼头，断鸿声里，江南游子。把吴h看了，栏干拍遍，无人会丶登临急。」寓意满怀理想的人在现实无用武之地，只能手执宝剑拍遍栏杆泄愤。华叔在前言中说：「我无栏杆可拍，也不会去拍，省下气力去做别的事。」<br />
<br />
华叔偏爱辛弃疾的词，对於他的作品有共鸣，但感慨并不一样。辛弃疾登临赏心亭，北望中原金人占领下的风雨；华叔说：<strong>「在香港，不必登临远望，也会知道北方每天的风风雨雨。」</strong><br />
<br />
字里行间， 78岁的华叔并不为老去而惊惧，深信壮志定会薪火相传。他这样写：<strong>「活着一天，就奋斗一天，抱着理想信念，直至瞑目，自有後来人。」</strong>他认为，知道理想信念，必会有後来人去实现的。<br />
<br />
华叔当时验出肺部有阴影，但仍然坚持一贯信念；前言末段写道：「我不知道自己手上，还有多少日子；但却知道，剩下的日子也必如过去那样度过。」<br />
<br />
末了，华叔作了一首绝诗明志：「一剑一箫甘澹泊，亦狂亦侠也温文。不流拍遍栏干泪，未悔无人倩袖巾。」</div>

							
							
							
							
