<br />
4、我的朋友就有从事警察职业的。他们的工作既非伟光正宣扬的那样“崇高”，也并不是时时都在“严防别有用心”，“警惕不明真相”，“打击非法上访”，“协助完成拆迁”这般猥琐――事实上，在有限的可能面临正确与错误、光荣与耻辱的选择机会中，我的朋友和他周围的同事，无论是出于法学素养形成的法治精神，还是出于朴素的善恶是非理念，大家都在尽自己最大的可能去避免“作恶”。绝大多数情况下，他们只是在做着一份工作，白天下片儿区职守，晚上值班巡逻，维护着社区的治安。他们也是普通人，也是端起碗吃饭，放下筷子骂娘，也在诅咒着高房价，张罗着结婚找对象，讨论着上黄网下A片――没有必要妖魔化警察，就像没有必要妖魔化群众一样。<br />
