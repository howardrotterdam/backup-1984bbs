<br />
2、承上，“贴标签”的标准相当多样。“身份”和“职业”就是其中很重要的一种分类标准。例如作为标签的“警察”，在论坛里通常对其有着“傻逼”，“流氓”，“该死”，“助纣为虐”……的注解。就像在官方话语体系中一样，“群众”作为一种标签，通常与“不明真相”、“被煽动”联系在一起；“记者”作为一种标签，往往也是“破坏安定团结大局”、“别有用心”、“以讹传讹”的代言人；公共知识分子都有着“西方自由主义倾向”、“言论哗众取宠”、“行为不讲政治”的劣习……从60年前起，依靠“地、富、反、坏、右”、“现反”、“资产阶级自由化”等一大批标签，葬送了一代又一代人的青春、爱情、生活、生命。我常常不怀好意的意淫：N年之后的某一天，是否我们也会如此这般，将流氓的“警察”、收红包的“医生”、洗脑的“教师”、原罪的“富人以及他们的二代三代”……等等等等的“标签人”，以“清算历史”的名义挂牌游街，然后组织公审公判，代表“人民”将他们一网打尽？如果真是这样，历史只会陷入毫无意义的轮回，我们将消灭“敌人”，然后剥下他们的皮囊套在自己的身上。现在的“老大哥”，就是未来的“大老哥”。<br />
<br />
