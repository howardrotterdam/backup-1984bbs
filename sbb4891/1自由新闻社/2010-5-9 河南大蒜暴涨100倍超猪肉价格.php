比肉贵、比鸡蛋贵、比白菜贵，你敢相信这还是大蒜吗？（资料图片）<br />
<br />
多重因素助推大蒜价涨<br />
<br />
作为中牟大蒜的一个经纪人，张扬州介绍，目前中牟发往郑州的大蒜8000多元/吨，每斤4元多,像2007年、2008年那种大蒜扔掉也没人要的场面不存在了。张扬州认为，如今蒜价之所以一路走高，主要在于市场供不应求。“前几年蒜贱伤农，大蒜种植面积此后几年大幅缩减，供不应求是蒜价上涨的重要原因。”<br />
<br />
据业内人士介绍,随着国际经济形势的好转，大蒜的出口也开始“回暖”，出口量增加了，也是诱发大蒜涨价的一个原因。<br />
<br />
此外,大蒜涨价也被部分业内人士认为是炒起来的。“大蒜的利润都被中间商赚了”，据一中牟蒜商介绍,越是涨价越是有人大量购买。<br />
<br />
新蒜上市后有望缓解供求<br />
<br />
“与最高时候比,今天蒜价每斤已降了8角钱,以后还会降,但是幅度不会很大。”刘庄蔬菜批发市场一商户也这样告诉记者。<br />
在张扬州看来,虽然现在有零星新蒜上市,但一周后中牟大蒜才会大量上市。<br />
“大蒜价格降到5000元/吨~6000元/吨才较为合理,也就是一斤大蒜售价2.5元~3 元,是正常价位。”<br />
<br />
杞县大蒜未收已卖1亿元<br />
<br />
5月6日,杞县裴村店乡孟里寨村蒜农张书庆数着2万块钱非常高兴,他种的5亩蒜还长在地里, 就被山东客商以每亩4000元的价格提前收购。据杞县农产办负责人说,全县今年种植大蒜40万亩,现在已有3万亩大蒜还没有收获就被外地客商提前收购,蒜农已经入账一亿多元。<br />
<br />
杞县是全国著名大蒜生产、加工、贮藏、销售基地县。杞县大蒜以个大、皮白、味辛辣、不散头、耐贮运等优点赢得了国内外客商的青睐。目前,全县大蒜种植面积达40万亩，产量60万吨，建成大型交易市场5个、保鲜和恒温冷库630多座、加工企业80 多家,在国内20多个大中城市设立了大蒜直销点，每年出口大蒜及制品25万吨。经过精心培育，“金杞”牌大蒜已成为享誉国内外的知名农产品品牌。据大河报链接》》<br />
<br />
有关部门称菜价涨幅大 因气候异常及油价上升<br />
<br />
近期一些地方菜价涨幅较大，消费者对此反应强烈。记者7日分别采访了商务部和农业部相关负责人，对菜价情况进行了解和分析，并对今后一个时期的走势作出基本判断。<br />
<br />
灾害性天气是导致菜价升高的重要原因。今年春天气温持续偏低，延缓了露地蔬菜生长期，促使蔬菜价格一直在高位运行。据农业部分析，从2009年秋冬季开始的低温雨雪天气抬高了蔬菜总体价格，此后价格一直高位运行。<br />
<br />
农业部市场司这位负责人还认为，从2009年下半年开始大蒜价格直线上涨，带动蒜苗、蒜黄等蔬菜价格也随之上涨，抬高了蔬菜价格整体水平。<br />
<br />
中商流通生产力促进中心分析师邵娜表示，油价上升致生产成本和运输成本的增加抬升了蔬菜价格的重心，促使今年蔬菜价格整体高于上年同期水平。<br />
<br />
商务部市场运行司副司长朱小良分析认为，即使是近期上涨较快的山东大蒜，也主要是因为受天气因素影响。<br />
<br />
据农业部统计，今年春播蔬菜播种面积增加。31个省(区、市)蔬菜意向播种面积1.34亿亩，同比增加263万亩,增长2%。目前我国蔬菜生产势头良好，市场供应有保障。随着气温回升，陆地菜上市，特别是一些生长周期短的叶菜将迅速填补市场空间，后期蔬菜价格有望继续回落。 据新华社<br />
