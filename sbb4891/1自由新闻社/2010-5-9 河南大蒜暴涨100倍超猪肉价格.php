2010-5-9&nbsp;&nbsp;重庆晨报<br />
<br />
“蒜” 你狠 超市大蒜卖到19元/千克<br />
<br />
家住郑州市农业路附近的娄女士在丹尼斯超市买了5个大蒜，花了5.5元。“一个大蒜超过一元钱,这也太贵了!”她把大蒜拎在手里看了半天，有点犹豫吃还是不吃。<br />
这间超市的大蒜标价为19元/千克，而猪后腿肉价格却为6.98元/500克，一斤猪肉拼不过一斤大蒜的悲剧就这样实实在在地发生了。<br />
<br />
一袋大蒜从2元飙至200元<br />
<br />
在郑州陈寨蔬菜市场里，48岁的南阳人周岩卖的大蒜最便宜：5.2元一斤。<br />
周岩是南阳人，在郑州卖中牟大蒜已经8年了。在他的印象中，大蒜从来没像现在这样“金贵”。<br />
大蒜现在卖5.2元/斤,这个价比“五一”前每斤下降了8角钱。“最贵的时候为4月中下旬, 卖到6元钱一斤。”老周说。<br />
“前年这个时候,一袋大蒜(40斤左右)批发价才2~3元，之后从去年新蒜上市价格一路飞涨，同样一袋大蒜，今年涨到200多元。”老周说,5月5日进的货还要180元一袋。<br />
<br />
根据老周多年的卖蒜经验，随着近期新蒜陆续上市，大蒜价格可能有所下降，但不会降太多。<br />
<br />
