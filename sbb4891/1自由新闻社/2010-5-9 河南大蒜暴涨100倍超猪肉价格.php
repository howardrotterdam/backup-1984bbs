来源: 华西都市报(成都)　<br />
<br />
两年前，一袋40斤的大蒜才两三元钱，今年这袋大蒜要价200元。河南大蒜身价短时间内暴涨100倍。<br />
<br />
家住郑州市农业路附近的娄女士在丹尼斯超市买了5个大蒜，花了5.5元。“一个大蒜超过一元钱，这也太贵了！”这间超市的大蒜标价为19元/千克，而猪后腿肉价格却为6.98元/500g，一斤大蒜的价格贵过猪肉。<br />
