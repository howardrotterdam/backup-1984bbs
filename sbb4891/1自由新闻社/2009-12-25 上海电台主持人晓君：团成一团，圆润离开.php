													<h2>上海电台主持人晓君：团成一团，圆润离开</h2>
						
						
																											<div id="postmessage_394726" class="t_msgfont">上海电台主持节目中说听众:请“滚”出上海(图)<br />
 来源：人民网 2009年12月25日<br />
　　12月23日晨8点47分，上海电台动感101《音乐早餐》节目正在直播。主持人晓君和小畅在播放音乐间隙，用上海话聊天逗听众。<br />
<br />
　　一名听众给节目热线发了一条短信：“求你们不要说上海话了，我讨厌你们上海人！”<br />
<br />
　　晓君在节目中语调认真地说：“……这位听众，请你以一种，团成一个团的姿势，然后，慢慢地比较圆润的方式，离开这座让你讨厌的城市，或者讨厌的人的周围。”<br />
<br />
　　因为年轻的电台主持人在直播节目中回击听众的一句话，“团成一团，圆润离开”成了沪上论坛的热门词汇。<br />
<br />
　　听众<br />
<br />
　　反应一：“这样说外地人比带脏字还狠”<br />
<br />
　　反应二：“来上海就要从心底里爱上海”<br />
<br />
　　12月23日早晨8点47分，上海电台动感101《音乐早餐》节目正在直播。与往常一样，两位主持人晓君和小畅在播放流行音乐间隙，使出浑身解数，用上海话聊天逗笑。<br />
<br />
　　一位听众给节目热线发了一条短信，据事后主持人晓君回忆，短信内容为：“求你们不要说上海话了，我讨厌你们上海人！”直播节目中，主持人晓君将听众的短信一字一句念了出来，然后语调认真地说：“……请你以一种，团成一个团的姿势，然后，慢慢地比较圆润的方式，离开这座让你讨厌的城市……”<br />
<br />
　　节目实录如下：<br />
<br />
　　晓君：在我们节目过程当中，就有一位朋友啊，他非常友善地发来了一句话说，求你们不要说上海话了，我讨厌你们上海人。<br />
<br />
　　……<br />
<br />
　　呵呵呵，非常可爱的这么一个人啊。我就觉得，首先，请你以一种，团成一个团的姿势，然后，慢慢地比较圆润的方式，离开这座让你讨厌的城市，或者讨厌的人的周围。<br />
<br />
　　小畅：其实呢我一直都不明白，他一边说讨厌，为什么一边还要待在这里。<br />
<br />
　　晓君：其实我觉得这是很搞笑的一件事，你不可能说你跑到日本，对日本人说，请你们不要说日文，我讨厌你们日本人……作为都是中国人啊，何必不尊重人家的文化呢，这样的人相信跑到哪里都不会受人尊重的。<br />
<br />
　　“团成一团，圆润离开”一语惊煞了申城数以万计的听众。当时正在开车收听动感101的张小姐就是其中一位，她说：“当时我有点被雷到了，反复想自己是不是听错了。”在肯定自己所听属实后，她有些不忿道：“一个主持人，哪怕不带脏字，这样说外地人，比带脏字还要狠。”<br />
<br />
　　也有人对主持人示以支持,网友KOKI在电台论坛上发帖，称：“模子（方言，即“牛人”）！说出了我们的心声，不尊重上海文化就要‘团团’。”也有网友在论坛上跟帖，“要来上海就要学做一个真正的新上海人,打从心底里爱上海,以上海为家,入乡随俗。”<br />
<br />
　　晓君<br />
<br />
　　回应一：没有因“团团说”感受到压力<br />
<br />
　　回应二：直播中并未使用“不雅词汇”<br />
<br />
　　无论支持还是反对，听众们都担心，主持人会因“团团说”惹上麻烦。<br />
<br />
　　昨日（12月24日），早报记者联系到主持人晓君，晓君回应说，至少到目前，他都未感受到来自领导和主管的压力。<br />
<br />
　　晓君全名阚晓君，1987年出生的他毕业于上海戏剧学院播音主持专业。由于外形俊朗，主持风格幽默，目前在沪上小有名气。<br />
<br />
　　晓君坦言，做节目以来经常收到类似的短信留言，矛头直指上海文化。“以前我们都是置之不理的，但那天看了之后有点生气，就把它念了出来。”晓君解释说，“初衷就是质疑一小部分人，比如不喜欢你的人来你们家，谁也不会笑颜以对吧？！”<br />
<br />
　　对于在直播中回击听众，晓君强调，自己并未使用“不雅的词汇”。在当天节目中，晓君和搭档解释，很多来上海的外地人都非常友好、善良，都是可爱的新上海人，而“不尊重（上海）文化”的仅是个别现象。<br />
<br />
　　专家观点<br />
<br />
　　听者要宽容 说者要调整<br />
<br />
　　对于这场有些尴尬的风波，上海大学社会学系教授邓伟志认为，双方均有些“片面性，太冲动了”。<br />
<br />
　　邓伟志强调，在多元社会中，区域歧视是常见但不应该的现象。“外地人要清楚上海人对城市的付出，上海人也要明白外地人给城市带来的贡献。”<br />
<br />
　　对动感101主持人晓君的“团团说”，邓伟志称，面对极个别听众的“挑衅”，作为大众传媒掌握话语权的主持人要注意措辞，要体谅。<br />
<br />
　　其实除了“挑衅”，短信也对节目提出了少说方言的建议。根据相关法规，除非得到行政管理部门批准使用方言，广播节目应以普通话为基本用语。对此，上海戏剧学院主持艺术专业吴洪林教授称，适当夹杂一些方言可以增加节目特色，“地方台节目允许一些方言，但如果比例超过一半，就是不允许的。”<br />
<br />
　　对此次风波，吴洪林教授总结说：“听者要宽容，说者要调整，主持人应该更严格要求自己，发言也要谨慎。” （记者 李伟）</div>

							
							
							
							
