上海向来是一座海纳百川热情好客的城市，我作为一个祖辈籍贯为江苏的严格意义上的新上海人，我父母是抱着能制造一个能为上海城市发展作出杰出贡献，甚至帮助祖国更好更快建设的美好初衷把我诞生在了上海。“城市的高度它越变越快，上海让我越看越爱。”在上海蓬勃发展的过程中有越来越多的和我一样的外来人员渐渐融入了上海，接受了上海的文化，并且切实为上海的发展做了很多，爱上了上海。对于这样的人，我致以最崇高的敬意。可是现实总是残酷的，总有一些人由于个人“不读书不看报不去上海书城”的原因，导致找不到工作、游手好闲甚至走上违法犯罪的不归路。这种人他们不自信，所以他们总会觉得是上海这座城市以及这个地方的人在处处排挤他们；这种人嫉恨一切自己无法融入的生活方式（包括语言、礼节、行为、饮食等），一切常人看来正常、美好、亲切的东西，他们都试图诽谤、毁灭。所以我抱着一丝幻想能拯救这些人，帮助他们走出内心的阴霾、建立健全的人格，劝他们要学会融入所在的社会集体，实在不行就团结起来，慢慢学会圆润的做人，离开这座你看不到希望的城市。人的智慧在于希望与等待，难道不是吗？<br />
<br />
当一个外国人在你面前大骂中国人的时候，有民族荣誉感的人会怎么做？当一个陌生人跑到你家来大骂你全家的时候，有家族荣誉感的人会怎么做？周立波说“乃一组特”，我不敢像波哥那么直接，我不动手，动嘴，我试图挽救这些心理扭曲的人。我想，仁至义尽了吧？<br />
<br />
我们欢迎优质的非上海人，也欢迎自谦为非优质但热爱上海愿意融入上海的非上海人在上海自信彪悍的生活。<br />
<br />
上海人以及融入上海的可爱新上海人都是有荣誉感的，是有使命感的，是有大局观的，如果仅仅因为我说了一句得体的大实话而被断章取义、抨击抹杀的话，那这样的媒体环境实在太可怕太可悲了，我无法融入，我一定会和我的志同道合者团成一团，慢慢的圆润的离开我所讨厌的地方。<br />
