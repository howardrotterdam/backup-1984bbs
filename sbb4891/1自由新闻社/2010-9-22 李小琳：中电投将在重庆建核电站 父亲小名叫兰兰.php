																											<div id="postmessage_839498" class="t_msgfont">李小琳：中电投将在重庆建核电站 父亲李鹏小名叫兰兰<br />
2010年09月21日 13:12华龙网―重庆商报<br />
华龙网讯 坐在酉阳县龙潭镇万寿宫的大厅里，她像一位小女孩，缠着一群老年人唱一首又一首的土家山歌；行走在赵世炎烈士纪念馆里，她当起了讲解员，讲述着她舅爷赵世炎、奶奶赵君陶鲜为人知的故事；饭桌上，她时不时会用重庆话与人交流……她就是中国电力[1.77 1.72%]投资集团副总经理、中国电力国际发展有限公司董事长、被称为“中国电力一姐”的李小琳。 <br />
<br />
她见到记者的第一句话是：“我们的约定兑现了吧，不仅我回来了，还把我女儿兰兰(小名)一起带回来了！”今年3月，全国两会期间，李小琳接受本报记者专访时，约定今年一定要回酉阳老家看看。9月19日晚8点，李小琳接受了本报记者的独家专访。 <br />
<br />
乡情家事 <br />
<br />
李鹏与外孙女小名都叫“兰兰” <br />
<br />
重庆商报:今年3月，您说一定要回酉阳看看，今天终于回家了，您感觉如何? <br />
<br />
李小琳:非常兴奋，昨晚(9月18日)在酉阳的宾馆，我是一夜未眠，脑海中全是奶奶、父亲给我讲的故乡的故事，还有奶奶的形象。半夜三四点，我实在睡不着，便把女儿叫醒，给她讲起家乡的故事。女儿也是第一次回家乡，听我讲故事，她也兴奋得睡不着。(这时她指指自己的脸，笑着问:是不是有点憔悴？) <br />
<br />
其实，我很多年前就想回家乡看看，但一直没能成行。这是我第一次回酉阳。酉阳是我奶奶赵君陶的故乡，她是对我人生有着重要影响的人之一，在奶奶、父亲的影响下，我爱吃榨菜、海椒。我们在酉阳捐建了一所希望小学，孩子们写信叫我来看一看，有几次都准备出发了，又因有事未能成行。2000年，我从四川到重庆，行程中曾定好了到酉阳；2001年父亲、母亲回酉阳，最初也是确定我陪他们；2005年到重庆，车都准备好了，没想到最终还是未能成行。 <br />
<br />
我下定决心，今年再不回老家，就不能原谅自己。父亲多次告诉我：“要常回家看看，那里是你的根，去看一看家乡的变化，为家乡建设做点贡献。”几天前，我告诉父母，我要带女儿兰兰回酉阳，父亲兴奋地说：“应该，应该！”　<br />
<br />
因此，这次回家主要是三件事，到龙潭老家、启动“熏陶”基金给师生颁奖、签订风电投资项目。 <br />
<br />
重庆商报:您女儿小名叫兰兰，听说是为了纪念您父亲的小姨赵世兰？ <br />
<br />
李小琳:(笑)其实我们家还有一个人小名叫兰兰，那就是我父亲。我奶奶赵君陶，是家中的老九，小名老九。当时，赵世兰和赵君陶姐妹同在一起，工作上互相帮助、鼓励，生活上赵世兰对小妹赵君陶既有姐姐的关怀又有母亲的慈爱。那时赵世兰已有30多岁了，她决定终生不结婚献身革命事业。赵君陶深为敬佩，她与姐姐商定，自己有了小孩，不管是男是女，小名都叫“兰兰”，即用赵世兰名字中的最后一个字来起名，以表示永不忘记姐姐赵世兰。后来生下一个男孩(即李小琳父亲李鹏)，小名也还是遵守约定，用了个女孩的小名叫兰兰(李小琳说着便忍不住笑起来)。 <br />
<br />
我很小的时候，奶奶带我去给赵世兰姨奶奶拜年，她给的两元压岁钱到现在还留着。后来，我生了女儿，为了却老人们的心愿，更是为了纪念两位革命老人，我们便给女儿取小名叫兰兰。 <br />
<br />
这次带兰兰回老家，一方面是认祖，另一方面接受教育，让她看看前辈们为了革命所付出的鲜血和生命，让她知道兰兰这个名字的内涵。 <br />
<br />
重庆商报:回北京后肯定会讲述回老家的经历和感受吧? <br />
<br />
李小琳:肯定的。父母2001年回酉阳到现在，已有9年时间。这9年来重庆、酉阳变化太大了。到酉阳通了高速，通了航空，这在9年前是不可想象的。 <br />
<br />
父亲对重庆是格外关注的，三峡工程、重庆直辖是他们这一代领导人做出的重大决策之一。三峡工程建得如何，重庆发展得怎样，他都非常关注。重庆GDP每年的增幅是多少，财政收入增长是多少，甚至两翼农户万元增收、户籍制度改革、公租房建设等他都了解得非常清楚。　<br />
<br />
教育情结 <br />
<br />
李鹏300万稿费捐给教育 <br />
<br />
重庆商报:我看过您写的一篇文章《长相思》，是写您奶奶、我国著名教育家赵君陶的，文章非常感人。 <br />
<br />
李小琳:这是一篇纪实性散文，我认为是我写得最好的文章。奶奶在1985年去世，虽然与我一起相处的时间不长，但她是对我人生有着重要影响的人之一。奶奶给了我无比温馨的童年，奶奶经常在游戏娱乐中教我诵读唐诗宋词、四书五经。在奶奶家简朴的四合院，我从小见识了不同类型的人。 <br />
<br />
爷爷李硕勋28岁被叛徒出卖遇害后，奶奶曾任延安保育院院长，悉心抚养年少的父亲和不少烈士的孩子，奶奶坚强的革命意志给了我深刻的印象。在奶奶的影响下，我看了《牛虻》、《钢铁是怎样炼成的》等名著。 <br />
<br />
重庆商报:这也是您成立“熏陶”基金来怀念他们的初衷吗？ <br />
<br />
李小琳:是的。我们一家都有教育情结。我奶奶赵君陶从事教育几十年。我父亲除了重视教育外，还用实际行动支持教育，他用自己的稿费在万州、涪陵都捐建了希望小学。前不久，父亲将他的300万元稿费又捐给中国教育基金会。母亲是全国关工委副主任，经常做关心下一代的工作。 <br />
<br />
2001年，我筹建了李硕勋教育发展中心，在10年时间里，先后拿出150万元，奖励(或资助)了北京、四川等地300名品学兼优(或贫困)的学生，50名优秀教师。几年前，我开始筹划“勋陶“(取自李硕勋、赵君陶名字的最后一个字)基金，后经过思考改为“熏陶”基金，希望熏陶下一代，培养下一代。 <br />
<br />
重庆商报:9月19日对酉阳10名教师、50名学生的奖励，既是回家的一份礼物，也意味着“熏陶”基金的正式启动，是吧? <br />
<br />
李小琳:是的。李硕勋教育发展中心过去没有对重庆的师生进行奖励，“熏陶基金”可以弥补这个遗憾了。我希望是每年颁一次奖，主要奖励品学兼优的学生和优秀教师，以及资助贫困学生，这也算是爷爷、奶奶爱国家的一种延续。 <br />
<br />
记者 谭柯 实习生 万婧 采写 <br />
<br />
中电投将投资千亿在重庆涪陵建核电站 <br />
<br />
重庆商报:今天，你们和酉阳县签约，投资24亿元建风力发电厂，是不是意味着中国电力投资集团、中国电力国际发展有限公司已将重庆作为投资热土，这只是一个开始? <br />
<br />
