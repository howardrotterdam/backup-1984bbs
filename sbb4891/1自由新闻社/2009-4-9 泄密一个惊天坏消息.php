													<h2>泄密一个惊天坏消息</h2>
						
						
																											<div id="postmessage_71435" class="t_msgfont">作者：时寒冰<br />
<br />
最近，来信太多，无力一一回复，以后选择性的集中回复。――寒冰（暖之） <br />
<br />
1.问：如何看股市？ <br />
答：超过7成的股票有阶段性见顶迹象。 <br />
<br />
2.问：农民如何应对未来的通货膨胀？ <br />
答：养猪。存粮。 <br />
<br />
3.问：保8能否成功？ <br />
答：能。一定能。 <br />
问：你这样确信？ <br />
答：我对统计部门从来都有信心。 <br />
<br />
4.问：次贷危机谁受害最深？ <br />
答：中国。 <br />
问：谁先复苏？ <br />
答：美国。 <br />
问：为何很多人看法正好相反。 <br />
答：表演看多了。 <br />
<br />
5.问：你如何看茅于轼廉租房不建厕所？ <br />
答：他可能把厕所当成奢侈品，把拉屎尿尿当成高消费了。 <br />
问：茅于轼认为，经济适用房给人带来了贪污腐化的机会，就应该停掉经济适用房。 <br />
答：各行各业都存在贪污腐化的机会，都应该停掉。 <br />
问：其实，只要有人类存在就有腐化机会啊？ <br />
答：根据茅老的理论，把人类消灭就可以彻底解决这一问题。 <br />
问：那茅老呢？ <br />
答：他留着。他是品德高尚的人，永远不会腐化。 <br />
<br />
6.问：《中国新闻周刊》报道说，北京大学司法鉴定室主任孙东东表示“对那些老上访专业户，我负责任地说，不说100%吧，至少99%以上精神有问题――都是偏执型精神障碍”，你如何看？ <br />
答：这位教授精神可能有问题，很可能是偏执型精神障碍。 <br />
问：能否提出一些治疗建议？ <br />
答：建议看兽医。 <br />
<br />
7.问：你如何看中国人民银行行长提出最终取代美元的“超主权储备货币”的设想？很多人评价说是中国伟大的战略调整。 <br />
答：看不懂。 <br />
问：为啥？ <br />
答：一边积极购买美国国债等，一边要废美元，所以看不懂是啥战略。 <br />
<br />
8.问：你如何看潍坊市寒亭区采用下发“红头文件”的形式，向该区副科级以上干部摊派“售房任务”？ <br />
答：官商勾结的模板。房子卖不动了。 <br />
问：最近卖得不是很火吗？ <br />
答：集中演出的回光返照。 <br />
<br />
9.问：应对通货膨胀，为何不把买房作为首选？ <br />
答：房价未作充分调整，所含泡沫会冲抵防通胀功能。 <br />
问：你同意买房不如买地产股的说法吗？ <br />
答：如果是自住，买房（垄断不改，早晚都要被掠夺）；如果是投资，从波段操作角度看，买房不如买地产股，而买地产股不如买资源类投资品――长期看如此。 <br />
10.2009年4月6日的《人民日报&#65533;海外版》报道说：北京市朝阳区东八里庄的一个居委会里，组织一批老年人到几个售楼中心“签约”，一天管吃喝还净挣50元。据介绍，这些老年人有时还要到国贸春季房展会上演“订房”，前台签单，后台拿钱走人。在央企工作的钟先生，前几日去亚运村北苑家园附近看一个楼盘，由于售楼中心人头攒动，他一下就动了心，可销售人员对他说，好房已经没有了，要买好的，需托熟人找经理批条。钟先生请客送礼，到这个项目的经理办公室批了一张1%优惠条，待到看见售楼处半尺厚的优惠条后，他才恍然大悟：原来“热销”、“抢手” 都是导演的。你如何看这则新闻？ <br />
答：开发商很孝顺，北京的老年人很幸福。 <br />
<br />
11.问：北大教授孙东东认为老上访专业户“至少99%以上精神有问题”，这一事件如果发生在80年前会怎样？ <br />
答：北大学生会要求校方开除他，校方也会断然将其开除。 <br />
问：现在呢？ <br />
答：现在的北大学生决不会做这种傻事。 <br />
问：如果真这样做？ <br />
答：学校可能会留下孙，把极个别“闹事”的学生开除。 <br />
问：鄢烈山撰文指出，在三鹿毒奶粉事件发生后，孙东东说“奶粉保险、企业主动、政府处理没有疏漏”，批评孙东东见人说人话，见鬼说鬼话。你怎么看？ <br />
答：鄢烈山说错了，人家明明是见人见鬼都说鬼话的。 <br />
<br />
12.问：如何看公立医院转民营医院？ <br />
答：一些人要借医改的掩护发财。将来看病更难、更贵。 <br />
问：新医改为何把公立医院为民营医院放在前面？ <br />
答：想发财的人等不及了。 <br />
<br />
13.问：宿迁市宿城区暗访干部，发现干部随口说谎，一位乡镇工作人员中午违反规定饮酒，却说：“我是养……养鸡的！”如何看干部随口撒谎？ <br />
答：说谎？这么实诚的干部已经不多了，这位干部可重用。 <br />
问：安排什么工作岗位？ <br />
答：养鸡。 <br />
<br />
14.问：如何把握黄金投资？ <br />
答：顺势而为，波段操作。 <br />
问：你在2009年1月16日写的《投资感言》一文中，推荐过两只股票（中金黄金和山东黄金），2月24日，又宣布提及的“个股自动作废”，是一个波段吗？ <br />
答：是的。前者是一个买入点，后者是一个卖出点，是一个完整波段。2月20日，国际金价见阶段性顶部，至今尚未超过这一高点。2月24日，国内黄金股票见阶段性顶部，向下调整了20%。黄金投资波段性操作才能确保利润最大化。 <br />
问：黄金能否长期持有？ <br />
答：从长期来看，在目前价位买黄金的收益肯定小于其他有色金属（不含贱金属）。3月22日在知钱俱乐部近三个小时的讲座中，已经详细分析了这个问题。 <br />
<br />
15.问：你如何看近期备受关注的公职人员涉嫌嫖宿年幼女生案不公开审理？ <br />
答：为了保护公职人员的隐私。 <br />
问：为何伸向在校学生的黑手越来越多？ <br />
答：犯罪成本太低，在校学生越来越弱势。 <br />
<br />
16.问：美元会崩溃吗？ <br />
答：不会。 <br />
问：为什么？ <br />
答：没有任何一种货币可以替代美元。没有任何一个国家像中国这样热衷于英雄救美。 <br />
问：防通胀持有哪种货币最好？ <br />
答：持有货币不能应对通货膨胀，所有货币都会贬值。 <br />
<br />
17.问：日本占我钓鱼岛，其他国家占我南海许多岛屿，你如何看？ <br />
答：我们忙准备阅兵，顾不上搭理他们。 <br />
问：阅兵后是否会收复？ <br />
答：不知道。 <br />
问：这个可以知道。 <br />
答：这个真不知道……不清楚还有没有其他活动安排。 <br />
<br />
18.问：你批评茅于轼老先生的是否偏激？许多人同意茅于轼先生的观点，去掉厕所富人不感兴趣，是有利于穷人的。 <br />
答：富人侵占经济适用房与廉租房，都不是为了自住，而是为了出租或转卖，不含厕所的保障性住房照样让富人有利可图，他们仍会设法侵占。解决这一问的关键不是去掉厕所，而是提高透明度，比如，通过名单公示的方式，让全社会参与监督，这类事情就可以减少。 <br />
问：如果没有效果，茅于轼老先生下一步会建议什么？ <br />
答：你去问茅老。 <br />
问：你猜猜？ <br />
答：逼我躲猫猫啊。茅老可能会要求把厨房拿掉，如果再没有用，把屋顶拿掉，再没有用，把墙也扒掉……茅老说了，这房反正是给穷人住的。 <br />
