现将我于2006年6月7日在深圳第一看守所写的材料作若干补充，再次控告如下：<br />
<br />
（一）我曾是许宗衡的座上宾，亲眼目睹他的若干丑态。<br />
<br />
2004年10月底，任深圳市常务副市长的许宗衡通过他所谓嫂子陈萍等，把我请到深圳，安排在威尼斯酒店和我见面。当时许宗衡一见我，象小丑般样地作揖、拥抱。甜言蜜语地让我尽力帮助他当市长，我们从此认识。<br />
<br />
我送他一本我主编的《今日湖南》和文选，他说他看过我的书，仰慕得很。他请我吃饭之际，专门叫他老婆小苗过来作陪。他夫妻流着眼泪诉说他们当官之苦，开始要拜我为师，要效古人磕头，被我几次阻拦。后便求我和他结拜，我勉强答应。从此，他叫我哥，偶尔也叫师老师。<br />
<br />
他甚至下贱地拉住他老婆的手和我的手说：“今后你对大哥要比对我还要好才行，你要敢对大哥不尊，我就和你离婚。”他老婆也演戏，故作耍娇状：“你以后再欺负我，我就找哥。”<br />
<br />
我当时就感肉麻，觉得他不象党的干部而象江湖黑帮的角色。由于礼貌，也不得不和他逢场作戏。（见附件一的部分照片。）<br />
<br />
很快，我发现他交往了多年的嫂子陈萍假话太多，提醒他注意。他让我今后单独和他来往，并把他所有的电话都抄给我，还把他妹夫杨维明和他扶植的一家湖南风味餐厅的老板介绍给我，说：“他是我大哥，今后大哥来不管我在不在，你们全力招待”。<br />
<br />
当时还觉得他很豪爽，回北京后向有关领导推荐他，以为他如果当了市长会干得更好。没料到他另有企图。<br />
<br />
一直到2005年5月他当了市长，我和许见了十多次，每次都是他和他妹夫安排招待，住威尼斯酒店或五洲宾馆，他和他妹夫轮流陪我吃饭，多次表示：“只要我当了市长，和你当一样，深圳有什么事就找我，我一定办好，一定要让大哥满意”。<br />
<br />
这些，我的司机桂永枫以及威尼斯酒店的苏小龙等人都可作证。<br />
<br />
他曾经恬不知耻地指着苏小龙对我说：“小龙对我非常好，比我的老婆还关心我。你有什么事情可以找她，你记住，我说的是不管什么事都行。”苏小龙也半装生气：“你要卖我吗？”<br />
<br />
我当时惊讶得很。觉得他们的关系超出范围，但是并没有多想什么，以为他们是湖南同乡，总是从好的方面考虑他的活动。<br />
<br />
（二）初露狐狸尾巴<br />
<br />
2005年9月，学历为哲学和经济学博士的深圳市国土局土地交易中心书记李德全，向我反映中海集团在横岗区买了一块土地多年，一半经当时为组织部长的许宗衡关照于2002年第7次会议批为商品房用地已建成优质小区，另一半原说在第一期开发完成后再变更，但是至今不批。后来我才知道，当时许宗衡就和所谓嫂子陈萍秘密交易，让她把他搞成市长后再批。由于我及时发现他嫂子的骗局后，提醒许宗衡断绝了他们的联系。但是我看了他们的报告后认为，中海集团的要求完全合理。（见附件2）李德全等人说：“这里面黑幕重重，你要出面找许帮助解决，这完全是合理合法的”。<br />
<br />
9月下旬我到深圳，许派司机把我从车站接到深圳五洲宾馆。我向许提到此事，他说：“这块地我知道，他们找你找对了，找任何人我都不会批，如果批给你，就等于送给大哥起码两亿多钱呀！”他见我不明白，就解释说：“中海买的是工业用地，当时每平米不过二百块钱，商品房用地现在起码是二千多块钱，你说他们挣了多少。陈萍和中海的人早就找过我，我一直压着。”<br />
<br />
我说他们已经买了，申请改变用地也是可以的，他说：“土地问题现在很敏感，没有利益谁愿给他负责，不过大哥出面，我也可以按历史遗留问题处理。我是七人规划小组组长，可以说批准，也可以说否定”。我说那你就批了吧。他问：“他们找你办事，给你报酬了吗？”我说：“朋友帮忙，我从来不提这事，君子言义不言利”。他马上说：“那你就不理睬他们，别管他们的事”。我说：“你就全当是帮我吧。”他说：“那你起码得向他们要两千万，你有了钱，我没钱时问你要。”我说：“可以呀。”他很认真地对我说：“真的，你可不能给他们白干，一定要钱。我看你很可靠，跟你合作我放心。你问他们要二千万，看中海给不给，他们有的是钱。只要给，这事好办。如果你不好要，让他们找我妹夫。”当时他还指桑骂槐地说：“陈萍夫妇太贪，我给他们办了许多事情，他们并没有给我什么好处。你想他们吃肉我喝汤的事情还能办吗？”<br />
<br />
我对他产生了反感。后来，我把许的妹夫杨维民的手机号给了中海深圳公司的经理。我不好把许的话转告，觉得太丢人。<br />
<br />
（三）被许宗衡利用陷害我的两件事的真相<br />
<br />
2005年11月底，李德全对我说：“有一个真正的亿万富翁想见你，请你吃饭。”我同意了，于是认识了李映元。当晚，李映元请我和李德全到阳光酒店旁他开的歌厅看他的实力，李德全对我说：“他歌厅的负责人是原来深圳公安局刑侦队的，下海挣钱了。”李映元也说：“市里许多领导经常来，许宗衡过去也常来，当了市长后来的少了。”李映元想拉我下水，让我带小姐回酒店玩被我当场拒绝。我对此很警觉，同时拒绝了他请我当顾问的请求。告诉李德全：“我不想和这种打交道，以后我再不见这种人！”<br />
<br />
与此同时，李德全还给了我一份关于申请横岭污水处理厂水量保底和解决泥外运问题的报告，是说香港一家姓林的在横岗建厂后，由于区领导变更，他的厂没有人支持，需要领导支持，同时让我帮他们搞些项目。（见附件3）我认为这是环保的好事，就答应帮忙，让领导重视，同时，我也帮助他们在北京找房地产开发的项目。<br />
<br />
与此同时，他还给了我一些其他的报告。<br />
<br />
李德全对我说：“你给他们办事，办成了会感激你的。为了让你放心，我让他们给你汇点信用金。办不成事，信用金必须退还他们”。我再三表示不要，说：“朋友帮忙要什么钱，我可不是陈萍。这些事情你看着办吧，但是要办好。”<br />
<br />
后来，我在北京时，他给我电话，告诉我他给我的银行储蓄卡里存了些钱，要我查。他说：“你怎么用都可以，算借、算给都行。”但是为了稳妥，我把钱交给了香港和我搞出版社的合伙人，对他强调：“钱保存好，不能乱用，随时有可能退给他们。”以后见了他和林木成等人后，我再三表示不要钱，林木成确实提出了借给我的事情，我看当时他们盛情所在，实在无法退回，准备将来办完事再说。<br />
<br />
很快，我把李德全交给我的报告，分别批上我的意见，亲自交给了许宗衡。<br />
<br />
我绝对没有想到，正是这些钱在我和许宗衡宣布决裂后竟被他歪曲成所谓诈骗的“证据”。<br />
<br />
（四）许宗衡开出了办事的天价<br />
<br />
2006年春节前夕，许宗衡派司机将我从深圳富苑酒店接到五洲宾馆。见许时，我又把李德全给我的几份报告给了许，许说：“你看这个污水厂，如果我们把他救活，他投资的两个亿保住了，每年少说也要挣几千万。横岗区每年的环保经费几个亿。让这家公司起码给你八百万。不然，他们会拿你当傻瓜。过年他们也不给你点收获，只要你有收获，我愿意帮忙。”我离开时，他送我一瓶洋酒和湖南产的一块菊花石。<br />
<br />
过完春节上班后不久，我到深圳后，许让他妹夫杨维民把我和我的司机接到市政府的餐厅吃午饭，政府的一位秘书长作陪。饭前，许宗衡把我拉到一边谈话，我提到那几份报告的事问：“是不是为难？如果违规违纪就算了。”他说：“为难什么，看对谁呢。大哥让办的事，为难也得办。不过绝对不能便宜了那帮人。深圳的情况，你大概还不了解，从来没有白办的事，这是市场的规矩。陈萍（他的所谓嫂子）怎么发起来的？还不是我支持。他再三追问：“他们到底给了你多少？你要对我说实话。”我说：“一分钱也没给，事情办完了，他们会感谢的。”许提高声音：“办完了给，别相信他们的话，必须先给。”说着又讲了许多例子。最后他说：“报告先放在这里，告诉他们我会处理的。”吃饭时，在场的人不断敬我酒，许把他那份鱼翅、燕窝都让给了我。<br />
<br />
此后十多天没动静，我于2月中旬来到深圳见许宗衡。邱秘书通知我上午10点到办公室。我到后他办公室坐着一个北京什么医药基金会的女同志也在等他。她给了我一张名片。一直到12点，他的秘书说市长在市委那边有客人，我陪你们楼上吃自助吧。<br />
<br />
吃完饭，许回来了。他把我先叫到他办公室谈，他大骂几句市委书记后才把话转到我来的话题上。他说：“我让你问他们要钱，实际上是为了你，他们应懂得游戏规则。你不要骗我，你真的没得他们的钱？”我说：“真的，我是从政治上考虑问题的。我给你的报告，都是有道理的。”他说：“那也不能白帮他们。就说我吧，你知道我当市长，每月的开销有多大？我儿子在英国留学，花销也很大。说出来能吓你一跳。我把你当大哥才和你这样说，换了别人我见都不见他。你是作家，要钱合法的。”我说：“我们是纯洁的朋友。”许宗衡说：“朋友也得讲规矩。中海的事不能少于两千万。你后拿来的这些，最少也得三、四千万。我让杨维民和你配合，他懂，内行。哥，我确实拿你当作自己人看待呀，换成别人办只会要的更多。这些钱，要下都是你的。我需要时从你这儿拿。你要不好意思，把钱交给我妹夫，你什么时候需要向他要。这也是你办，其他人办，我是一律拒绝，坚决不办。”我把材料放下后，说：“我考虑考虑怎么向人家开口。”他笑了。“告诉他们，钱少是敲不开门的。”<br />
<br />
这时，我都不想再找他了，可是看到几个公司着急的样子，加上李德全对我说：“大哥，只有你他还买帐，你办不成别人更不行了。”为了维护许，我不想过早把许的嘴脸暴露。所以到2月底3月初，我又带上重新复印的原来的几份报告见许。<br />
<br />
邱秘书让我11点到办公室。这天，许很高兴，见面就说：“李德全提拔的事，我已经给组织长打电话了，让他们考察”。我问他那几份报告的事怎么处理，他说：“走到哪里办这些事也得花钱呀。问他们总共要三千万一点都不多。就是给我办事也得花钱呀。”他见我不吭声，又说：“许多事，我比你更清楚。你毕竟是作家，不在体制内。你和我合作干上几件事，你这一辈子就再不为钱的事发愁了，也不用这么辛苦的奔波了。我让我妹夫跟你合作配合，要下钱你得大头，我和我妹夫得小头。（意思是三人分）。”<br />
<br />
他问我其他事情，我已没心思和他打交道了。我问：“你到底什么时候批下去？我好向他们有个交待。”<br />
<br />
许说：“你让他们到国土局查我的批件吧。我中午有客人宴请，不能陪你吃饭了。下次来再请你吃饭。你告诉他们准备钱吧，拿到手后和我妹夫联系。记住，一定不能白干。”<br />
<br />
这次谈完，他和秘书一起送我到了电梯口，还关心地问我的身体状况。<br />
<br />
（五）我忍无可忍，宣布和许宗衡再不往来，宣布和他决裂<br />
<br />
2006年3月19日我到了深圳，马上和许联系。晚上，许宗衡给我打电话，让我明天一早（即3月20日）到他办公室。第二天我准时到达，等了几分钟后他和另一个才来上班。<br />
<br />
进了办公室他关住门，问：“钱还没给你吧？小心上当！”这时，我对他由失望到反感了，就冷冷地说：“我考虑再三，总觉得不合适，我开不了口。”他很干脆地说：“那就不给他们办。”我反问：“你不是答应先批到国土局吗？”他说：“批是要承担责任的，我不得他的好处，凭什么承担责任，你别犯傻。”我问他：“你就不怕出事？这样对你很不好。”他说：“现在官场都是这个行情。我的口特别紧，我也认为你很可靠。咱们合作出不了事，何况你得大头。”<br />
<br />
我火了，气愤地说：“那你就看错人了，我决不会这样干，宁可不办！”他也说：“那就不办吧。”<br />
<br />
我站起来，指着他说：“你这样干，迟早要出事。如果你这样，我们绝交，今后我再不和你来往了。”<br />
<br />
说完，我从他办公桌上拿上我给中央写的《发展和建设社会主义强国的若干意见》扭头走出他办公室，自己下了楼。当天下午我往广州赶，在路上我给他打了个电话，说：“许市长，我可是一直维护你的。”他说：“我知道。”我又说：“怪不得一直有人告你，我明白了。今后你出什么事，和我可是没有关系。”他说：“我在路上呢。”就把电话挂断了。<br />
<br />
接着，我给他发了一条短信：“过去我对你说的话和承诺，统统作废，望谅，师东兵”。<br />
<br />
此后几天，他给我打电话，我没再理他，这便使他对我更加怀恨在心了。<br />
<br />
（六）许宗衡对我秘密布控，秘密调查，最后公然动用专政工具进行拘捕<br />
<br />
4月17日下午5点40分左右，山西侯马市委的王震给我来电话，说：“深圳市公安局政保处的三个人在侯马公安局的陪同下到宣传部调查你，问你干过什么违法犯罪的事没有，我们说没有，还问你为什么那么有钱，问你的儿子和女儿调到了哪里，我们说不知道。你可能得罪什么人了。”<br />
<br />
这时，我彻底明白了，许宗衡要对我实行报复了。但是我反复思考，我没有任何把柄在他们手里，他能够把我怎样？不作亏心事，不怕鬼叫门。<br />
<br />
但是，我没有想到，就是清白人，鬼也敢叫门，岂但是叫，简直是破门而入！<br />
<br />
2006年4月25日，我和李德全等人电话联系，准备到深圳处理善后事情时，当日下午5点多就在五洲宾馆就遭到了深圳公安局刑事侦查局第六大队的突然拘捕。6月2日，经深圳公安局局长李锋下令，正式逮捕。<br />
<br />
他们刚抓我时，口口声声说他们掌握了大量的证据，我问他们要证据和证人时，他们竟然拿不出一点。我现在才清楚：他们完全是在没有我半点犯罪证据的情况下，采取先拘捕、先定性，然后再取证的下流、非法手段，把我这个具有真实姓名、真实住址、真实经历、真实办事过程及正常经济往来的作家，扭曲、陷害成“诈骗犯”。<br />
<br />
这完全是他们的犯罪活动。至今没有任何所谓的受害人对我指控，也没有任何我的所谓罪证。整个材料完全是他们强奸人意，自编自导的丑剧。<br />
<br />
<br />
<br />
（七）抓我后，他们根据搜走我的银行卡，进行了大规模的查寻活动，使用各种卑鄙手段“取证”，从几十人里搜罗到五个“证据”，然而没有一个是真实的。这是一起地地道道的强奸人意的陷害案。<br />
<br />
<br />
<br />
从2006年4月25日下午五点半被深圳公安局据传到26日晚十点关进看守所到9月30日取保候审，在历次的审讯我都提出一个基本的要求，即让所谓的受害人理直气壮地站出来揭露我、指证我，和我对质。<br />
<br />
按道理说，这个要求不但不过分，而且合情合理合法。但是办案人员却怕得要命。他们鬼鬼祟祟，俨然把我和所谓受害人的关系当作了共同犯罪加以防范，活生生地暴露出他们装神弄鬼的陷害本色。<br />
<br />
尽管办案人恬不知耻地装出一付“救星”的嘴脸，但是受害人们并不领情。他们要比深圳公安局更了解师东兵，他们更善于识别真假骗子。“受害人”们相信师东兵是一个讲信用、重感情、不贪财、有正气的真正共产党人，所以不管办案人如何花言巧语、玩尽鬼蜮伎俩，就是不上当。<br />
<br />
正因如此，我关押在深圳第一看守所时，他们不给我真正看病，不给我吃对症药，派出死刑犯和重刑犯对我百般迫害和威胁，企图害死我。<br />
<br />
总之，他们陷害我的每一步都得到了许宗衡的指使和请示，都是在许宗衡及其李锋等人的直接策划下进行的。我希望上级有关部门进行调查，依法予以处理，不使害人者逍遥法外。<br />
