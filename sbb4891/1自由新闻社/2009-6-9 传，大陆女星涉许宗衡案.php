<br />
许宗衡伙同前深圳公安局局长李锋等人颠倒黑白、为掩盖他们自己的问题而一手策划的陷害我的先发制人的诡计，恰恰暴露了他们穷凶极恶的政治骗子和腐败透顶的贪官嘴脸。<br />
<br />
为了置我于死地，深圳市公安局把和我有正常经济往来的李德全等人在抓我的当天进行了长达十多个小时的“询问”，逼迫和利诱他们对我“举报”，充当所谓“受害人”。为此，许宗衡甚至亲自重新启用真正的骗子陈萍，再次对她夫妇称兄道弟，让她对李德全“做工作指控我是“诈骗”，并许诺给他当副局长以诱哄。<br />
<br />
为了害我得逞，许宗衡几乎使用了一切最无耻、最卑鄙的伎俩。其实，他栽赃陷害我的东西，恰恰是他的真正症结所在。他把他十分精通的买官卖官的伎俩和活动手段提供给李锋一伙，让有关人对我进行以葫芦画瓢的调查，实际上就是他干的那些见不得人的活动的翻版。戳穿他的把戏，许宗衡才是货真价实的买官卖官的老行家，老干家，是典型的靠高阴谋诡计起家的政治骗子。<br />
<br />
一、许宗衡是买官起家，卖官发财的老手<br />
<br />
许宗衡是一个当面说人话，背后行鬼事的两面派。2006年7月14日的《深圳特区报》头版发表报道《许宗衡在市委党校中青年干部培训班座谈时语重心长地勉励学员，树立正确名利观一心为民办实事》，夸夸其谈“上的健康，下的痛快”，摆出一副谆谆教诲别人的架子欺世欺民。这样的肉麻的语言，在党报上已经很少见到，但是在许宗衡把持下的深圳这个特区却屡屡出现。这绝非偶然。<br />
<br />
在许宗衡的把持下，深圳的报纸不择手段地拼命为这个个人野心家造势，恬不知耻地美化自己，已经达到了登峰造极的地步。他的一切活动都是为了表演给领导机关和舆论看的，他和他的同伙所组织的一切经验，用内行的话说，根本就不能用，他们自己也从来不计划使用，统统是为了骗人，都是这伙骗子的把戏而已。他们的一切目的，都是围绕着捞权和捞钱而进行的。<br />
<br />
其实，他从湖南衡阳落选窜到深圳后，从当海天出版社的社长<br />
<br />
到当上市委组织部部长，再当上常务副市长，最后爬上市长的位置，他没有一次是健康地上来的。他千方百计地通过别人认识我，本身不正是为了“不健康”地往上爬吗？（当初我错误地以为他是喜欢我写的书，因为常常有许多干部是出自这样的目的而找我聊天，研究问题。）<br />
<br />
2004年10月底他通过别人介绍认识我后，就公开地当着好多人的面对我说过：“现在没有关系根本上不去，我到这个地步不知花了多少钱呀。”为了取得我对他的信任，他曾经连喝五大杯茅台而让我以茶代酒以表“诚意”，他以“交心”为名，大骂许多领导同志，包括帮助和提拔过他的人。在他眼里，什么共产主义理想和党的事业，统统“都是假的”。当我表明我是坚定的马克思主义者后，他说：“大哥，像你这样的人可是越来越少了。我敢说，年轻人里没有一个你这样的人。我儿子根本就不信什么共产主义，全是胡说。”<br />
<br />
当时，我原以为他虽然在理论和素质方面不高，但可能是一个努力工作的实干家，但很快我就发现他其实对经济工作毫无真知灼见，全是凭借一些新名词现买现卖地胡言乱语，他到了那里，翻来复去就是那么几句话。他的目的只有一个，那就是不择手段地往上爬，要官要钱。他在未当市长前，对我说：“我就是不惜一切代价也要当上这个市长，我已经投了不少资了，现在已经豁出去了。好多企业家为了我当市长，都愿意豁出老本。”我曾经试探过他的口风，结果他所说的并非醉话，而是千真万确的事实。再后来的事实，我已经彻底地看清他的面目，他原本就不是一个好人而是一个典型的、地地道道腐败分子。<br />
<br />
许宗衡自己就对我说过：“这些人出钱帮我当官，我得还债呀。现在大的工程都是李鸿忠把持，我不好插手也不好多问，只好部署一些改造工程。实际上抓好了也能挣不少钱，只要不搞成豆腐渣就行。他们能想办法给他们挣钱的工程了。”他多次说：“我这个市长当得也很难呀。”我当面批评他：“怎么能这样搞呢？这样下去要出问题呀。”他说：“他们不都是这样搞吗？李鸿忠和黄丽满搞的那些工程严格地说，没有一个是合格的，都大大地超出了预算，他们能搞为什么我不能搞？”他曾经多次在我面前说：“对我们来说，根本没有能搞不能搞的事情，只有搞得巧妙不巧妙之分。那些一搞就让人发现抓住尾巴的人，就是不搞也会垮掉。”<br />
<br />
他的伙伴为了购得某公司，拿出一个亿的钱通过一个香港商人从中运作，许宗衡呼应配合，终于弄到了一块4万多平米的土地。之后，许宗衡经常到他家里聚会，成为他的保护后台之一。此人的公司头衔很多，他和许宗衡巧妙地勾结大肆进行权钱交易，已经达到出神入化的地步。<br />
<br />
就在2006年12月24日，许宗衡亲自出马参加这些人会议并在会议上讲话，公开说：“这个组织对深圳的发展建设作出了重大的贡献。”这类组织究竟对深圳市有多大的“贡献”，姑且不讲，对他的贡献却是无法用数字来统计的。一个深谙内幕的人对我说：“许宗衡在外国留学的儿子是靠这类组织和商人给他提供资金的，许宗衡利用权力为这类组织和商人提供的好处一般人根本无法想象。”而且这类组织据我了解同样是真正带有黑社会色彩的洗钱、腐蚀干部和进行种种不法活动的组织。这才是西方社会真正典型的黑社会集团。<br />
<br />
同时，许宗衡也亲口对我说过：他和某集团的关系也是不同寻常的。这家企业实际上就是他的妹夫杨维民和他直接伸手的“取款机”。<br />
<br />
为了向上爬，他身边也培养和寄生了一批靠此起家并为他牟利的人成为他的党羽，其中不少就是货真价实的真骗子，如我已经揭露过的陈萍夫妇。他们以此在深圳各区、各地巧妙地进行形形色色的违法乱纪活动。<br />
<br />
他和他的同伙还亲自扶植了一批所谓的企业家给他提供资金，其中就有不少是搞土地起家的人。他们在许宗衡活动市长的过程中也为他提供了大批的资金。许宗衡曾经当面对我说过：“某城是我的基地，这里的老板我始终阿护。我和他们的关系，放心，永远出不了事。其他的那些人，荣超呀卓越呀，都是黄丽满支持的，我和他们交往是留了一手的。但是，也可以放心。事情到了和他们利益一致的时候，就不大可能出事了。这点，我是研究过的，有经验。”他多次在我面前说：“大哥，我别的本事不多，但是我的口特紧。黄丽满就是看准了我这一点。当初我们联手搞某某的时候，也是动用了一些老板。这些都是我给他顶着呢。”<br />
<br />
此外，为了买官，他巧妙地利用一切合法和非法的手段敛财。深圳的老贪官们，利用审批土地之权把大批的土地早就卖给了一批所谓民营企业家和不法商人手里，许宗衡上台前后通过封官许愿和承诺让一些不法商人和投机分子给他提供资金，他上台后用审批改变土地和转让土地的名义给这些人作为回报。在我向他提供了南油公司的报告时，许宗衡提出了索贿的要求被我拒绝，他坚决不给南油公司审批报告。说穿了，许宗衡玩弄的这套把戏，就是用共产党的钱来买共产党的官，然后再来巧取豪夺地捞人民的钱财。就拿李德全来说吧：其实我在不认识许宗衡和李德全之前，他们通过许宗衡的所谓嫂子陈萍就认识了。李德全当初把自己仕途希望寄托在当时任组织部长的许宗衡身上，对他竭尽一切表现之能事。但是，对于许宗衡来说，他并不看一个干部的能力和政绩而是要钱的，恰恰李德全拿不出多少钱来“孝敬”他。于是，李德全提拔的事虽然经过陈萍的周旋而一推再推，一直到许宗衡当了市长还没有下落。<br />
<br />
骗子陈萍倒是不断地对李德全进行敲诈、勒索、盘剥和哄骗。打着许宗衡和别人的旗号向他要这要那，我向许宗衡揭露了陈萍的嘴脸后，许宗衡表面上和陈萍划清了界限，不再往来。同时我也向李德全揭露了陈萍的骗子本质。这时，李德全已经清醒，向我诉说了他跑官以来的上陈萍一类人的当和花费巨大精力的事情。出自对李德全的同情和对一个博士干部的爱护，我才向许宗衡推荐了他。而且我和他说得十分清楚：“我推荐李德全是从政治上来考虑的，就是为了不让一些谋私的骗子从中取利搞鬼。”<br />
<br />
在我和许宗衡没有决裂之前，许宗衡答应并且向市委组织部推荐了李德全。<br />
<br />
许宗衡以小人之心度君子之腹，以为我也和他一样，推荐李德全必然是得了他的钱财。所以，在他的贪婪嘴脸彻底暴露后我出自愤怒宣布和他决裂，他终于露出凶恶的面孔，悍然动用专政工具，指示深圳市公安局将我抓起来然后再定性取证。但是他还是失算了。他没有想到的是，所有的“受害人”包括李德全不但不认为我是骗子，反而更加清醒地看出了许宗衡这个真正骗子的丑恶本相，纷纷起来揭露他们玩弄的陷害我的罪行。<br />
<br />
这种结局不正说明了民心不可欺的历史逻辑吗？<br />
<br />
二、骇人听闻的卖官价格表<br />
<br />
许宗衡是一个为当官而迷了心窍的野心家，也是一个卖官而近乎疯狂的不择手段的巧伪人。短短的十多年来，许宗衡从到深圳之后官位一直在上升。他的周围聚集着一帮为他谋官、跑官的团伙，也同时是一帮为他卖官敛财的帮凶。他利用职权把他的妹夫杨维民安排到某公司专门为他敛财，小舅子安排到深圳市口岸管理中心当了主任的助理，其实已经指挥动了主任。他的外甥张星安排到罗湖区国税局当了科长，他利用职权通过各种手段为他的亲属们牟取利益。而他们也依靠许宗衡而拼命进行捞钱的勾当。他买官卖官的团伙中除了他妹夫杨维民、骗子陈萍等人外，还有一个所谓的副总裁。此人专门给他拉关系、送钱送东西作为他升官的经费，同时到处寻找“猎物”而活动他们买官。许宗衡和他之间的密切来往，相互捧场，都是为他活动而开路。许宗衡深圳利用此一类的人给他不喜欢的人书写匿名信进行诬告和陷害一些领导人的活动，其中四、五年前深圳市出现的告某等人的各种信件，就是在许宗衡等人的密谋和支持下出现的。<br />
<br />
买官卖官，他们甚至公开列出的卖官标价是：一名区的正职不低于1000万；大集团正职不低于800万；一般的局长在500万到600万之间。许宗衡曾经对我说过这样一句话：“在一些场合，只要我出来，其实就是给他们作广告。”这个深谙此类诡计的家伙，同时也不断用此方法给他的妹夫杨维民进行这种造势活动，过去他约我到深圳，每次都让他妹夫出面，就是为了这样的目的。<br />
<br />
与此同时，许宗衡为一些人大肆包揽一些大的工程进行倒卖而洗钱。他向国土局、规划局打招呼让某中标，承包了盐田港添海工程捞了五个多亿。又亲自活动让他中了南山区旧城改造的工程，等等。许宗衡从中得了多少钱，一些内情人估计在上亿数目上考虑。我们相信，通过细致的调查，许宗衡的犯罪活动一定会大白于天下。<br />
<br />
当我为一些朋友的事情找他帮忙，并再三告诫要依法依规行事，我曾经书面批道：“应该在法律和政策的范围内帮忙，不行就明确给个答复，不必为难。”谁料他竟然也想把我作为替他升官、索贿、谋财的同伙来交易。通过近八个多月的时间我逐步认识到他的面目，并掌握了他的许多非法活动的蛛丝马迹后不得不在2006年3月20日公开宣布和他决裂、不再往来。<br />
<br />
本以为此事到此结束，没想到他竟然在不到一个月的时间里密令深圳市公安局对我监控调查，在没有任何证据的情况下凭着监控而得到的一些短信来捏造罪名对我陷害逮捕，企图置我于死地。公安局长李锋指使办案人对所谓受害人李德全进行长达十多小时的询问中，恬不知耻地说：“你不承认师东兵诈骗，就是你买官卖官给师东兵的钱。”李德全回答：“我和师东兵在一年多的时间里断断续续才有十万来块钱的交往，包括他让我兑换港币的钱，难道一个副局级干部这么点钱能买上吗？”<br />
<br />
办案人员哑口无言。<br />
<br />
铁的事实充分说明，深圳市在许宗衡一类贪官污吏的把持下的买官卖官的价格，早已经成为公开的秘密。这也是许宗衡在市能源公司谈话时，对主要领导人说：“不管你再能干，不会给领导擦鞋、带来利益，照样不能用”的真实含义。为了控制效益好的国有企业，许宗衡亲自安排主要干部，如深圳市能源公司，他把他的秘书陈敏生安排成主要领导，巧立名目捞钱，成为他的一个“秘密钱柜”。<br />
<br />
三、某一类的改造工程，实际上是许宗衡<br />
<br />
一伙洗钱、捞钱的腐败工程<br />
<br />
众所周知，利用大搞公共基础建设进行洗钱、敛财，已经是官商勾结的腐败方式。许宗衡亲自为不法商人开路承包工程而偷工减料、采取非法手段上演合法中标等等，是他的拿手好戏。<br />
<br />
许宗衡批地、办事、提拔干部打招呼，主要针对三种人：一是给他提供活动资金或给他钱的人；二是他认为对他的升迁而有利的人；三是通过他的亲属、帮派而对他有好处的人或者他有求于人的人。除此，他是要讲“原则”和“大道理”的。此人善于作秀，惯于表演，长着一副八哥式得到甜嘴巴。认为你有用，见了男的便叫哥，见了女的便叫姐，是什么样的动作和丑事都可以作出来的。为了用你，他说哭就哭，说笑就笑。在工作中，他经常讲些新名词，总结一些口号式得到东西。其实真正要问他工作的含义，他连半点也说不上来。这一点，我是深有感触的。<br />
<br />
许宗衡在刚和我认识的期间，曾经这样对我说：“大哥，深圳到处是黄金，也到处是蟊贼。只要咱们弟兄合作，我保证你今后再不缺钱花。只要我批给你一个工程，让你一个工程中标，你转包出去就是几百万上千万。你要学会作生意。”当时我没有看透他的用意，就说：“我还是当作家搞点研究可以，作生意白垩商务的强项。”他露骨地说：“单纯当作家应该改行了，不必要专门作生意。你和我妹夫合作就够你一辈子花了。”现在回想他当初的许多话，使我今日联系深圳市许多的所谓重点工程，都可以看到许宗衡一双双黑手在肆无忌惮地掠夺着、吞噬着国家和人民的财产。<br />
<br />
他曾经对我说过：“在深圳没有自己的人是什么也干不成的。”他确实也在拼凑着一个专门谋私捞钱的小集团。早在他当市委组织部长的时候，已经开始了这样的活动。他从搞党务工作到常务副市长，实际上就是冲着市长的位置去的。作为一名基层领导干部，如此蓄谋的捞权谋官，在我们党的队伍中已经罕见。许宗衡完全是一个有计划、有预谋、有后台、有势力的腐败小集团。查清和消除这个隐患，对纯洁广东乃至全国党的干部队伍，都是具有重要意义的。<br />
<br />
2005年他当市长不久，他带队考察香港时，通过一个港商认识了中旅集团的老总。别人介绍此人神通广大，同时介绍人给了他一笔费用（具体数目待查），他回到深圳后修改了城市规划，把市民中心对面的原本是绿化一块土地批给了中旅集团盖办公大楼。对此，他曾经对我说过：“只要对我有好处，同时也是我看准的人，我是敢于负责任的。对我没有好处，我凭什么要冒哪个险？我给人批的土地和改变用处的，我有我的道理，前提是不能白干。”<br />
<br />
也就是说，许宗衡是敢于滥用权力的最恶劣的腐败分组。什么规划、什么法律和纪律，只要他认为需要干完全可以不受任何约束。所以，许宗衡这类坏人也可以凭着掌握的权力欺骗、拉拢一些领导干部为他说话、掩盖其犯罪。他和财政局长乔家华多次到北京找人活动，为他们的升迁进行活动。他确确实实是在用共产党的钱、国家的钱来买官卖官、进行违法乱纪。所以，他对自己的违法犯罪有恃无恐。乔家华的一个弟弟在获悉中纪委对许宗衡调查的消息后公开对一些干部说：“许市长在北京有人说话，到了关键的时刻会有人给他说话、铺路的。”<br />
<br />
四、许宗衡和许多坏女人相互勾结<br />
<br />
许宗衡是什么人？就连他的所谓嫂子陈萍的丈夫朱树法在他把我抓了以后的2006年6月下旬，许宗衡重新宴请这对骗子后，背转身评价他是：“见人说人话，见鬼说鬼话的大骗子。许宗衡色鬼！”<br />
<br />
陈萍自己曾经说过：“凡是经他安排工作的女孩子都和他上了床了。这个家伙是钱、权、色都要的家伙。”当许宗衡一段时间里不和她来往时，他曾经准备好了告发许宗衡的材料，后来许的老婆小苗出面进行了一些交易，才封住了陈萍的嘴巴。<br />
<br />
许宗衡在我参加的一次吃饭场合，以半开玩笑的语言说：“男人如果不抽烟、不喝酒、不玩女人，还不如死了呢。”他见我对此摇头，又故意显得坦诚地说：“这些话我只是在大哥和自己人面前说，到了外面就要被人抓辫子了。”<br />
<br />
2006年9月30日深圳市检察院被迫对我取保候审后，许宗衡一伙深怕他们的罪行败露，急不可待地通知陈萍夫妇于国庆节后连车带人从深圳“撤退”躲避，同时让和他和密切关系的一些人暂时躲避起来，同时连续召开有关人开会，订立攻守同盟，销毁有关证据，同时统一口径，对有关人继续做工作，企图我反咬一口，继续对我进行陷害。<br />
<br />
其中一些民营企业由于和许宗衡有不可告人的勾当，在获悉有关部门调查后，市政府有人通知他们 “立即出外躲几天，现在深圳风声很紧。”同时，陈萍也给地税局副局长某人打电话：“师东兵已经出来了，有关我们和许市长的任何事情都不要讲了，一旦出事就是大事。”她还和一集团的老总说：“许宗衡说，他最大的错误就是看错了师东兵，原来以为他是一个书呆子，把许多事情告诉了他，害得许宗衡这段时间根本睡不好觉。”<br />
<br />
五、许宗衡为了向上爬，不择手段地诬告和陷害干部，他是背后教唆书写匿名信的后台之一<br />
<br />
许宗衡是一个十分狡猾、阴毒且又伪装豪放的骗子，他多年来混迹官场，在深圳市当组织部长多年，有一定的活动市场，耳目较多。多年来，他为了向上爬，对一切阻碍或反对他的干部是恨之入骨的。他在当组织部长的时候，就和一些人勾结在一起，反对当时的一些比他官大一点点的领导。他有话不在党的会议上发表，而是和一些人串通大搞背后活动。当时他认为，某人是他们进行捞钱或升迁的阻力，千方百计地串通一些人搞他，捏造了他通过某中央领导的子女出卖深圳市一家医院给外商等问题，秘密指使陈萍和他的小舅子等人书写匿名信向一些部门反映。据陈萍讲：“许宗衡搞某的材料是他写好让我们打印由他的小舅子和我的司机等人发出的，我为他上台是立了大功的。他现在不理我，有他好看的。”后来他把我抓后，迫不及待地和陈萍夫妇重新勾结也证明了他们之间确实存在许多黑幕。<br />
<br />
事实也确实如此，在一次和我吃饭的过程中，许宗衡亲口讲：“某人如果不走，我们也会想法子把他搞下来的。光凭他和一家制造锂电池的公司的关系就可以搞掉他。”后来我通过采访查明，这家锂电池的公司并没有什么问题，法人代表确实是博士。<br />
<br />
这一点，从他后来陷害我的事实中已经得到了明证。许宗衡确确实实是一个十分危险的阴谋分子。我衷心希望党中央千万不要受许宗衡一类骗子的欺骗，认为深圳特区真的好得不得了。如今的深圳特区，实际上已经变成社会治安特别混乱，干部队伍特别腐败，正常办事特别艰难，漂亮文件特别多而其实特别不管用的所谓“特区”。<br />
<br />
必须看到，许宗衡具有丰富的反侦察手段，而且谈话一些人的勾结是共同犯罪的关系，查清他的事关系到揭开深圳市隐藏着的严重腐败的大盖子，他们必然要进行销毁罪证和订立攻守同盟。要查清楚他的问题虽然有不少困难。但是只要党中央下决心解决问题，我相信，随着时间的发展，许宗衡的丑恶嘴脸一定会彻底暴露的。<br />
