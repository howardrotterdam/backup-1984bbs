　　综合香港媒体报道，许宗衡多年来一直以“港深合作推手”的姿态活跃两地政治舞台，在香港媒体上有高曝光率，因此他“出事”的消息不单震惊广东政坛，也在香港引来极大关注。<br />
　　有评论指出，广东作为全国第一经济强省，中纪委将反腐倡廉的矛头指向广东，其实也是预料中事，因为广东蓬勃的经济活动，某程度可视为腐败的温床，而重锤在广东反腐倡廉，则能起抓典型之效。<br />
　　许宗衡能否安然度过这场廉政风暴，还是法网难逃，现时还是未知之数，但中纪委在广东掀起廉政风暴，却是不争的事实，今年以来，便有多名广东或来自广东的重量级人物被“双规”，包括曾任广东省委副书记、省政法委书记和省公安厅厅长的原广东省政协主席陈绍基，曾任广东省公安厅常务副厅长的公安部部长助理郑少东，以及曾任广东省纪委书记的浙江省纪委书记黄华元，他们均传涉及首富黄光裕案。<br />
　　事实上，许宗衡4月出席深圳市政府廉政、纠风暨绩效评估工作会议上便强调U“坚持一手抓经济发展、一手抓廉政建设，推动我市政府系统反腐倡廉建设迈上新台阶。”怎知他未抓廉政建设，自己却卷入廉政风暴。<br />
　　另有消息指，许案的揭发，除了之前外传涉及内地前首富、国美前主席黄光裕案、包括许氏太太“批发”深圳工程及大胆索贿外，还有一些更准确的说法，就是许的案情涉及了一些“香港因素”。<br />
　　<strong>消息称，许案浮出水面与内地一名女艺人有关；据说，内地一位艺人迁居香港后，经常往来于深港两地，结果引起有关方面的留意；有关部门结果发现她的动向，与许某有关。是许某托人一手安排该知名艺人到港，以及提供相关的资源。香港疯传著名影星周迅涉深圳许宗衡贪腐案。</strong><br />
　　过去4年，许宗衡曾多次主持深港合作会议，与多名香港特区政府问责官员会面，积极推动两地共同对抗金融海啸以及跨境基建、科技等领域的合作。<br />
　　就在早前，许宗衡曾公开倡议深、港在多方面合作，譬如推进两地金融合作、改善两地人员流动的便利化、推动两地跨境基础设施建设、推动两地科技协作，以及加强两地旅游、教育、文化、卫生及环保方面的合作等。<br />
　　消息人士指出，许宗衡推动深港合作的积极性、以及成绩是毋庸置疑的；但当前两地协作的大方向，其实是累积了多年的部署，且是广东省以至中央的战略，并非许宗衡的个人施政理想；因此许的去与留，不会影响深港合作。<br />
　　事实上，广东去年出台了《省委省政府关于争当实践科学发展观排头兵的决定》，国务院也先后批准了《珠江三角洲地区改革发展规划纲要》及《深圳综合配套改革总体方案》，这些文件俱有提及深港合作的内容，反映无论广东与中央，已有明确政策保障两地强化合作。<br />
　　中大经济系副教授庄太量悖表示，即使许宗衡被免职，也不会影响深港两地密切的合作关系，因为深港合作以至珠三角发展如港珠澳大桥等项目，都是中央政府已制定的政策，不会因一、两个官员的去留而改变。<br />
　　他相信，假若有关官员被免去职务，很可能由副市长代替，这样影响会较小；若由其他地方调任官员担任此职务，变数则可能较大。<br />
