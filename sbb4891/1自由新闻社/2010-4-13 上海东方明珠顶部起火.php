　　2010年4月12日23点,上海市气象台发布雷电黄色预警。<br />
　　4月13日,凌晨的黄浦江畔电闪雷鸣。2点20分左右,有目击者见到东方明珠(600832,股吧)顶部燃烧得像一支火柴。<br />
　　1小时20分钟之后,扑救工作基本结束。东方明珠相关负责人称,塔顶起火是因为强雷击中塔顶发射天线,引起天线外罩燃烧。<br />
　　作为上海的地标性建筑之一,高467.9米的东方明珠遭遇雷击已经不是第一次。雷电对高层建筑物的“偏爱”,并不阻碍人们对事实真相的探求:这次雷击事件,究竟是天灾抑或人祸？<br />
<br />
　　本期嘉宾:关象石,防雷专家,国际电工委员会IEC/TC81（第三工作组）中国专家。1945年生人,从事雷电防护研究近20年,在防雷标准领域有诸多成就。<br />
