　　半导体少长针消雷器<br />
<br />
　　在国际电工委员会标准中写道:“迄今尚无一种设备和方法能够改变大自然中的天气现象以阻止雷电的发生和雷击中建筑物或建筑物附近。”在我国实施的相关规范中,三类防雷建筑物防雷装置的效率分别为95%、90%和80%。<br />
<br />
　　“有人认为,建筑物安装防雷装置后就万无一失了。从经济观点出发,要达到这点是太浪费了。因此,特指出“或减少”,以示不是万无一失,因为按照本规范设计的防雷装置的防雷安全度不是100%。”《建筑物防雷设计规范》的条文说明第1条如此写道。<br />
<br />
　　因此,在上个世纪的最后几年中,一度火爆全国、自称能100%消灭上行雷（通常是由高建筑物发生的向上运行的先导触发的雷击）并将主放电电流渐弱99.9%的半导体少长针消雷器在学术界引起了争论。东方明珠采用的正是这种消雷器。<br />
<br />
　　自1995-1998年,我国学术界针对这一种产品,开展了一场大辩论。这场辩论被许多雷电专家称为“对防雷行业发展产生重大影响的事件之一”。1997年,在由北京市减灾协会举办的消雷器专题研讨会结束后,在京22名来自不同科研、设计、大学长期从事防雷工作的科技工作者联名撰文,呼吁有关部门从严从快整顿防雷标准、规范和产品市场。<br />
<br />
　　这22名科技工作者,包括我国的防雷鼻祖、现年90高龄的王时煦,国家标准《建筑物防雷设计规范》的起草人林维勇,负责研究“天安门、人民大会堂雷电保护”模拟实验的马宏达等人。<br />
<br />
　　关象石正是这22名科技工作者之一。他拿出一份当年22人联名致科技日报总编的一封信,其中写道:“贵报刊登了一篇《承九天雷电 保八方平安――半导体少长针消雷器开发推广启示录》。读了以后我们长期在建筑、电力等部门从事研究和运行的同志,对文中的观点和被歪曲的事实感到震惊。长期以来,国际学术组织均不承认消雷器,国际标准化机构也拒将此类产品列入标准。在国内,自消雷器问世以来,由于其没有科学依据,存在很大争议,尤其是未经科学试验验证其性能便将其大量推广向市场,应用于工程,致使国家财产蒙受巨大损失,令人感到痛心。为此,经多次讨论,兹提出我们的看法,请贵报予以刊登, 以正视听。”<br />
<br />
　　在这一场辩论之后,电力部门、邮电部等单位发文停止使用消雷器。但“四五年前东方明珠换过一次,换的还是这个消雷器的新产品”,关象石说这一次的雷击事件至少可以证明“消雷器没有消掉雷”。</div>

							
							
							
							
