																											<div id="postmessage_575117" class="t_msgfont">　高层建筑应做防侧击措施<br />
<br />
　　“东方明珠遭侧击是很正常的,并不是小概率事件”,关象石说。<br />
<br />
　　世界上最先被雷击的电视塔是被称为“欧洲第一塔”的莫斯科奥斯坦金诺广播电视塔,高573.5米。有人于1972年统计,它在4年半的时间里遭到了143次雷击。雷经常打在莫斯科电视塔中间的位置,这一现象引起了防雷学术界的关注,雷击电气几何模型的理论由此诞生。<br />
<br />
　　雷击电气几何模型决定,高层建筑可能会发生侧击,越高的建筑物遭到侧击的几率越大。在2000年版的《建筑物防雷设计规范》中,根据建筑物的高度对第一类、第二类、第三类防雷建筑物分别做了防侧击的要求。东方明珠属于第二类防雷建筑,规范针对这一类建筑要求“高度超过45m的钢筋混凝土结构、钢结构建筑物,尚应采取以下防侧击和等电位的保护措施”。<br />
<br />
　　但关象石了解到,许多建筑物在设计时并没有做防侧击,“有的是嫌不好看”。据他分析,这一次被雷击的部位就没有做防侧击措施。事件发生后,最让他担心的是上球体观光层是否做了防侧击措施。上球体观光层直径45米,高263米,是鸟瞰大上海的最佳场所,另有设在267米的旋转餐厅、DISCO舞厅、钢琴酒吧和设在271米的20间KTV包房。这个球体与此次被雷击的部位一样,都是玻璃钢外罩,“如果没有防侧击就麻烦了”。<br />
<br />
　　结语<br />
<br />
　　4月13日上午,北京市避雷装置安全检测中心和北京市消防部门表示,将在全市启动集中防雷检测,超过45米高层建筑成为重点。<br />
<br />
　　据了解,北京目前有超过24米或10层以上的高层建筑8592栋,其中超过100米的超高层建筑58栋,主要集中在西城、朝阳、海淀和丰台四个城区。一旦高层建筑发生火灾,如何救火是目前面临的一大难题。国内最先进的云梯仅约101米,远不及许多高层建筑的一半高度。防雷措施在这种情况下显得尤为重要。<br />
<br />
　　“要严格按照国家标准进行设计,以及通过监测,监测不合格就应该做增改”,关象石在防雷规范、标准领域做了多年研究和实践,他的书柜里满满的全是防雷标准和建筑的书籍。“我们每次都是出了事才亡羊补牢”,对于研究与现实的巨大差距,他只能笑着叹息。</div>

							
							
							
							
