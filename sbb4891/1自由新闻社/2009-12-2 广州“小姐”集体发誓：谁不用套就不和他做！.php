													<h2>广州“小姐”集体发誓：谁不用套就不和他做！</h2>
						
						
																											<div id="postmessage_352356" class="t_msgfont">“小姐”宣誓词<br />
<br />
“健康是人的第一财富，健康是幸福的根本保证，为了自己的健康和幸福，也为了他人的健康和幸福，从今天开始，我们一定要用安全套！每次都用！坚决使用！不用不做！”<br />
<br />
“小姐防艾歌”<br />
<br />
“父母养我几十年，离开家乡来挣钱。如果不用安全套，得病怀孕真是惨。打针吃药好几万，空手出门空手返。染上艾滋命归天，还要家人没脸面。”<br />
<br />
“健康是人的第一财富……从今天开始，我们一定要用安全套！每次都用！坚决使用！不用不做！”这不是在对某品牌安全套进行推广，而是一帮夜总会从业人员正在进行的宣誓！就在世界艾滋病日来临之际，广州市高危人群干预队（简称“高干队”），请来了国家级高危人群干预人员培训师、四川省皮肤病性病防治研究所所长万绍平开讲座。记者更是有幸跟随万绍平来到花都区一家夜总会，为数十名从业人员提供现场干预。<br />
<br />
八成娱乐业员工自觉戴套<br />
<br />
广州市疾控中心主任王鸣向记者透露，从2006年广州高干队成立以来，目前这一来自官方的干预队伍就已经壮大到了200人左右的规模，并覆盖全市各区、市（县级）。<br />
<br />
两年来高干队深入到夜总会、卡拉OK、酒店、发廊和出租屋等各类场所，为近2万名娱乐场所工作人员进行了以推广使用安全套为主旨的干预工作。而该中心艾防办的随访数据显示，这一系列活动目前取得了初步成效。“调查发现这一人群的安全套使用率已经达到了82.87%的水平，而现场为她们提供的HIV检测的阳性检出率是较低的，倒是诸如梅毒等性病患者不少”，市疾控中心艾防科科长徐慧芳向记者透露。<br />
<br />
专家自撰“小姐防艾歌”<br />
<br />
说起自己从事的工作，国家级高危人群干预人员培训师万绍平经常用“1个男人和4000个女人”的说法来描述。从上世纪90年代开展这一工作以来，他至今已面对面地为超过4000名娱乐场所工作人员进行过干预活动。在他的感召下，一定要用安全套在这一灰色的群体中得到了普遍的共识，而且不少干预对象洗心革面告别了以往灰色的生活。<br />
<br />
“到特殊场所宣讲预防艾滋病知识，如果总是重复地说理论，不会有人耐心地听下去，要从她们自身的健康、对后代的责任、对家庭的义务这些生活方面的细节入手，用震撼的事实来打动她们，用细节性的她们才会配合……”万绍平表示。现在防治艾滋工作主要就是哨点监测、VCT（自愿咨询检测）来发现病人；通过“高危人群干预队伍”去高危人群中主动发现病人和通过宣教普及提倡全民参与来发现病人这三种途径。但哨点监测和宣传教育的发现效率却很低，进行高危干预则更加主动、有效。<br />
<br />
为了取得最为直接的干预效果，万绍平甚至自己撰写了一首朗朗上口的“小姐防艾歌”――“父母养我几十年，离开家乡来挣钱。如果不用安全套，得病怀孕真是惨。打针吃药好几万，空手出门空手返。染上艾滋命归天，还要家人没脸面”，通俗易懂，动之以情、晓之以理。<br />
<br />
记者亲历<br />
<br />
她们为何愿意宣誓“用套”？<br />
<br />
日前在花都区疾控中心工作人员及当地有关部门的提前介入下，一群正处在开工前夕的“从业人员”被有组织的聚集在当地一夜总会最大的包房内。这些经常将“性”挂在嘴边，见到了乳白状液体就能直截了当说是“精子”的群体，对万绍平等一干拿着试管、宣传册的疾控人员的突然出现，起初表现得很抵触。<br />
<br />
“不生病多挣钱”<br />
<br />
“姐妹们，哦，应该说是妹妹们，大家看这1000000元现金，如果没有前面的1，后面是不是全都成了0，这个1就是你们的健康。我们来这就是为了你们的健康，让你们更健康、不生病、多挣钱……”一句非常贴切的开场白，双方的距离就这样缩短了。当万绍平拿出精心制作的各类性病患者乃至艾滋病患者的发病期各类图片时，三四十名“从业人员”变得鸦雀无声，凝神屏息的专注程度不下于听话的小学生。<br />
<br />
宣誓“坚决用套”<br />
<br />
现场干预取得初步成效后，万绍平开始了自己的“安全套推广”工作，他表示不用安全套会有“三大损失”、“三大误区”和“一个没想到”。损失来自健康、经济以及名誉方面，大家出来本来是挣钱来了，染上病以后，要花很多钱，最不合算，家人也跟着被人歧视。而误区则是外表健康不等于没有病；熟人不等于就是安全的；口交不会传播艾滋病。“一个没想到”就是坚决不用安全套的很有可能就是故意传播艾滋病、肝炎、性病者。<br />
<br />
之后，走南闯北多年已经有了4000人以上的干预经验的万绍平，趁热打铁专门向从业人员们介绍各地“姐妹”们用以对付不用套男性的多种策略。<br />
<br />
“拒绝‘做’是其中直接的一种，你还可以告诉客人我们这有一个多年来从来不戴套‘做’的女同伴，这还吓阻不了客人，你可以直接告诉他这个女同伴已经被发现染上了多种性病住院了……”万绍平介绍自成体系的一套拒绝方式下来，博得了现场一片领会的笑声。随后进行的“坚决用套”现场宣誓，立即得到了众多从业人员的积极配合。<br />
<br />
