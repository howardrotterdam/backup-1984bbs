　　在被问及有没有求教于语言文字专家时，叶满天说，有关主张是他自己想的，也没有那么深厚的古文功底，但是研究过《说文解字》等。<br />
<br />
　　叶满天主张把“嫖”改为“■”，这种改法实际上是造字。但他并不觉得造字有何不妥，因为历史上造字事例众多，而改造字在技术上现在没有问题，电子汉字输入系统已经很发达了。<br />
<br />
　　他还表示，自己的主张被有关方面采纳的可能性很大，但是需要时间。他本来有将相关主张“上书”的打算，文字材料也准备好了，但是还没有送交有关部门。<br />
<br />
　　叶满天不认为自己的行为是炒作，“我这是抛砖引玉，和炒作的目的不同，而且，如果这个主张被女同胞抢先提出，那么男同胞也容易被置于道德不义之地，会比较尴尬。” <br />
