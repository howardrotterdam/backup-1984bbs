<br />
2010年01月21日08:41<br />
<br />
<img src="http://img.hexun.com/2010-01-21/122433163.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
图为动漫海报《汉字很汗》。 来源：武汉晚报<br />
<br />
　　这几天，一篇题为《16汉字之错：既不尊重女性，又误导儿童人生观？》的文章出现于多家网站的讨论区，作者叶满天提出，有16个汉字歧视女性，应该加以改造，引起网友争议。毕业于南京某知名高校法律系、现为上海一家律师事务所律师的叶满天，昨天接受快报记者采访时认为，虽未将材料送交有关部门，但他的观点被采纳的可能性很大。<br />
<br />
　　律师：16汉字歧视女性<br />
<br />
　　《16汉字之错：既不尊重女性，又误导儿童人生观？》作者叶满天举出了16个歧视女性的汉字，分别为：“娱”、“耍”、“婪”、“嫉”、 “妒”、“嫌”、“佞”、“妄”、“妖”、“奴”、“妓”、“娼”、“奸”、“姘”、“婊”和“嫖”，他认为，这16个字，“均具有一定的贬义，让儿童在学习的过程中，让普通人在书写或阅读的过程中，从视觉上觉得这16个字与女性性别有根本的联系，无形中降低了他们对女性的评价”。<br />
<br />
　　由此，他建议改造这些字，并举例说：<br />
　　“嫖”，按照《现代汉语词典》的解释为：玩弄娼妓的堕落行为。“嫖”为形声字，部首“女”为形，“票”为声旁。常见词为“吃喝嫖赌”，都是形容人不务正业、游手好闲等恶劣品行。恶劣行为实施主体为男人，怎么可以在造字时把污水泼在女人身上？更何况这个字偏旁为“票”，在今天大多数人会理解为“钞票”的“票”，将“女”人和钞“票”放在一起，即为“嫖”，这给文字的使用者要传达什么信息？要传达一种什么文化？这个字只能说是中文之污。建议更改为 “彳不”，从字面上就可以看出是两个人做了社会不允许、不认可的事，相信每一个看到的人都会受到一次无形的教育，将来会有效地减少这种行为。<br />
　　他另外举的例子是“娱”和“嫉”，认为应该分别改为“彳吴”和“彳疾”。<br />
　　叶满天说：“基于同‘嫖’改为‘■’一样的道理，我建议‘奸’改为‘犭行’，可以向所有人表明‘犭行’是一种兽行。我相信更改这个字可以减少百分之二十的强奸(■)犯罪。”<br />
<br />
　　网友反对多于支持<br />
<br />
　　由于该文尚未在正式报刊刊登，所以还没有评论跟进，但是在网络世界，这个话题被吵翻天，在一片批评声里，不多的赞成声音显得很“孤独”。<br />
<br />
　　持反对意见的网友李鸥认为：从文字发展的历史角度看，由于历史上的重男轻女，导致了汉字中许多文字都有歧视女性的成分存在，这是客观存在的。但是，改变重男轻女以及其他歧视女性的落后旧习俗，绝不是将文字一改就能解决的，关键还是要靠思想文化教育和健全法制。<br />
<br />
　　不少网友则对叶满天的主张给予了嘲讽和斥责。针对叶满天“我相信更改这个字可以减少百分之二十的强奸(■)罪”的说法，一名网友说，照你这般，改的汉字多了去啦，那些不识字的强奸犯，又咋解释？一派胡言。<br />
<br />
　　一位网友表示支持叶满天：“有一定道理，至少比教育部的改法有道理。”这一网友所说的教育部的改法，是指去年8月12日，教育部公布《通用规范汉字表》面向社会公开征求意见一事。在这份征求意见稿中，不仅恢复了51个异体字，还拟对44个汉字“动刀整形”，调整其写法，引发各界强烈反响。<br />
