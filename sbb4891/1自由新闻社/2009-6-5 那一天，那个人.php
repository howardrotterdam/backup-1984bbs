Behind the Scenes: A New Angle on History<br />
By Patrick Witty<br />
<br />
Terril Jones had only shown the photograph to friends.<br />
<br />
While working as a reporter in Beijing during the Tiananmen Square protests of 1989, he shot many photographs and recorded several hours of video. It wasn’t until weeks afterwards, when he had returned to Japan, that he discovered the magnitude of what he had captured ― an iconic moment in history from an entirely unique angle.<br />
<br />
His version of the tank man has never been published until now.<br />
<br />
For 20 years the negatives rested in Mr. Jones’ belongings, following him across the world throughout his career as a journalist. He contacted The New York Times after reading the accounts of the other four photographers in Wednesday’s Lens blog.<br />
<br />
Mr. Jones’ angle on the historic encounter is vastly different from four other versions shot that day, taken at eye level moments before the tanks stopped at the feet of the lone protester. Wildly chaotic, a man ducks in the foreground, reacting from gunfire coming from the tanks. Another flashes a near-smile. Another pedals his bike, seemingly passive as the tanks rumble towards confrontation.<br />
<br />
The photograph encourages the viewer to reevaluate the famous encounter. Unlike the other four versions, we are given a sense of what it was like on the ground as the tanks heaved forward, the man’s act of defiance escalated by the flight of others.<br />
<br />
Mr. Jones shared his experience in an e-mail message to The Times:<br />
<br />
&nbsp; &nbsp; I was extremely high strung by June 5 when I took this photo. I had been running on little sleep since students began a hunger strike in Tiananmen Square on May 13, and I had been trading shifts with other A.P. reporters, staffing the square 24/7 for nearly three weeks.<br />
<br />
&nbsp; &nbsp; Adrenaline and the drive to stay close to the action took me back to the street on June 5. I was in front of the Beijing Hotel and I could hear tanks revving up and making their way toward us from Tiananmen. I went closer to the street and looked down Changan Avenue over several rows of parked bicycles when another volley of shots rang out from where the tanks were, and people began ducking, shrieking, stumbling and running toward me. I lifted my camera and squeezed off a single shot before retreating back behind more trees and bushes where hundreds of onlookers were cowering. I didn’t know quite what I had taken other than tanks coming toward me, soldiers on them shooting in my direction, and people fleeing.<br />
<br />
&nbsp; &nbsp; I stayed in Beijing for another month, until after Tiananmen Square and the Gate of Heavenly Peace were reopened to the public. It was only some time after I returned to Tokyo that, as I was going through my negatives, I printed this photo and noticed that I, too, had captured the so-called “tank man,” but from a completely different angle. He is small but unmistakable as he stands in the center of Changan Jie, clearly positioning himself for a confrontation with the approaching army. I was stunned to see him in my photo because his image had become a global icon of the events in Beijing. But I made the discovery several weeks after the fact, and the A.P. had already sent out a defining photo of that moment. So I filed away my picture, along with a couple of hundred more, and six hours of videotape that I had taken over three weeks of growing demonstrations.<br />
<br />
&nbsp; &nbsp; I never published them, and only showed them to a few friends and fellow reporters. But they were never far from my mind.<br />
<br />
&nbsp; &nbsp; I’ve always regretted not staying in place longer 20 years ago, despite the gunshots, and taken more photos, so that I might have realized what was unfolding before my eyes. But while I missed the timeliest opportunity to share this photo in 1989, today is an appropriate time to pull it out finally from its decades-old wraps.</div>

							
							
							
							
