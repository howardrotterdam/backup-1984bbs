甘肃藏族学生上街抗议中国统治 <br />
 <br />
据路透社报道，甘肃至少两个市镇发生藏族学生上街举行游行示威事件。据当地居民讲述，至少有20人被警方逮捕。 <br />
<br />
路透社报道称，本周三，甘肃省甘南藏族自治州首府合作市一些藏族高中生上街游行，抗议中国统治。报道称，在当地一家宾馆工作的一位职员说，她看到很多年轻人举行示威活动，至少有20人被警察带走。<br />
<br />
3月14日，同样在甘南藏族自治州的玛曲县也发生青年人上街游行。路透社的报道称，一位不愿公布姓名的当地一家超市的汉族经理说，这些青年呼喊支持西藏的口号。但是他不知道是否有人因此被捕。他说，民族问题是非常敏感的政治问题，他不愿意为此找麻烦。另外他还说，现在在街上可以看到很多武警，局势比较安全平静。<br />
<br />
报道还称，上周日开始，从玛曲通往70公里外的朗木寺的公路设置了多道关卡。<br />
<br />
路透社记者向甘肃地方外事机构和新闻办询问发生在合作以及玛曲的抗议示威事件时，两机构的工作人员都表示他们没有听说发生过抗议事件。<br />
<br />
2008年3月在藏区发生过多起大规模抗议事件，当时造成至少28人死亡。此后北京方面加强了在藏区的安全警戒，同时许诺加快这一地区的经济发展，希望能够以此平息藏人的不满。但是批评人士认为，如果中国政府不顾及藏人对其历史文化的诉求，那么藏区就难以真正实现稳定。<br />
