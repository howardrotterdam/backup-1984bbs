													<h2>张抗抗：《狼图腾》与科学发展观</h2>
						
						
																											<div id="postmessage_287773" class="t_msgfont"><a href="http://blog.sina.com.cn/s/blog_3ef9a7720100anhk.html" target="_blank">http://blog.sina.com.cn/s/blog_3ef9a7720100anhk.html</a><br />
 <br />
<br />
&nbsp; &nbsp; 20世纪90年代以来，中国的当代作家们，逐渐自觉地关注环境与生态问题，写作了大量探讨人与自然、自然与社会关系的文学作品。2003年，在国家环保局的关怀支持下，中国环境文学研究会曾组织评选了首届环境文学奖。有多部长篇、中篇、短篇、散文集和报告文学、诗集，共计几十位作家获此大奖，有力地推动和促进了中国环境文学的发展。<br />
　　2004年春，长江文艺出版社北京图书中心，出版了姜戎所著50万字的长篇小说《狼图腾》。这部书标志着中国环境文学的发展，无论从涉猎内容的广度、思想探索的深度、文学含量的浓度及质地密度上，都跃居到了一个新的高度。<br />
　　《狼图腾》的出现是令人震惊、振奋、欣喜的。面世后短短一周，已经引起了读者的强烈反馈。该书的主旨在于对中国国民性的反思与批判，根本立意落在“狼是人类社会进步的发动机”以及“中华文明从未中断的真正原因，是因为存在着一个从未中断的狼图腾文化”等历史文化命题上，并未深刻突出生态环保主题。但由于这部小说极其逼真地描述了70年代的草原游牧生活，形象地揭示了草原万物生态的内在逻辑关系，也是到目前为止中国第一部取自本土资源的“狼学”专著。同时，该书以几十个连贯的狼故事、以及主人公亲自掏狼崽、养小狼等生动丰富的细节，详尽地刻划了蒙古草原狼的性格特征，因此，我们有理由从环境文学的研究角度，给予其充分的重视和关注。<br />
　　这里，我想结合这次论坛的议题，来谈谈《狼图腾》一书中所体现的科学发展观。<br />
　　科学发展观的主旨是人与自然可持续的协调平衡发展关系。科学发展观必须继承和发扬华夏各民族在实践中产生，并经受了几千年历史考验，至今仍有巨大精神价值的优秀文化遗产。中国草原游牧民族的“狼图腾”文化，极为充分、透彻地体现了朴素的科学发展观。<br />
　　“狼图腾”是中国游牧民族几千年来从未中断的民族图腾。从古匈奴古羌族古犬戎族，一直到鲜卑、突厥、契丹、蒙古等民族，全都信奉狼图腾。“狼图腾”是中国游牧民族的精神力量之源，历史上曾攻占欧洲、打通了东西方通道、对世界震撼最强的东方人，都是崇拜狼图腾的民族。他们大都认为狼是自己民族的兽祖，狼是草原的保护神、是草原民族的宗师、战神、楷模。地球上没有一种动物，能像狼一样，对人的行为产生如此强大而持久的影响，并改写了世界和中国的历史，促进了民族的大交流和大融合。《狼图腾》一书中对此有翔实的考证和描述。<br />
　　我想从几个具体方面，来阐述《狼图腾》一书所表现的“狼”在保护草原生态、人与自然协调发展、控制草原万物平衡结构的杰出和不可替代的作用。<br />
　　一、“狼”一旦上升到民族图腾的崇高地位，便处于被保护的位置上。历史上，草原民族既打狼又护狼，建立了人与动物互相依存的循环关系。草原狼在饥饿的情况下虽然会咬死大量牲畜，然而，牧民对于狼仍采取保护措施，从不斩尽杀绝。“狼图腾”文化是人对动物、对自然生态的自觉敬畏与崇仰。千万年来，狼这种智性勇锐的动物在农耕地区几乎被捕杀灭绝，但在中国20世纪70年代之前的游牧地区，始终受到尊敬，野生狼群的存在使得草原的原始风貌被基本保留，所以中国在游牧势力强盛时期，一直没有呈现出沙进人退的荒漠化趋势。一直到20世纪50年代之后，由于汉族人口压力造成的粮食紧张，草原被局部开垦成农田；政府为发展畜牧业而鼓励打狼；狼图腾崇拜不断受到农耕文化的冲击和挤压，草原狼的逐渐消失与草原退化呈同步态势。中国的荒漠化、沙尘暴问题，主要是草原退化造成的恶果。草原是地球生命系统的重要组成部分，尤其对于中国华北的生存和发展，具有“一票否决”的地位和作用。一旦北亚蒙古草原以及甘肃、青海、新疆的绿洲彻底沙化，华夏文明就将被黄沙埋没。<br />
　　二、随着人口的恶性膨胀，汉族丧葬所用的墓地，将越来越挤占人类的生存空间。但是在草原上，千百年的狼图腾崇拜时期，草原从未受到墓葬的破坏。草原民族认为狼会将人的灵魂带上腾格里（天），所以将尸体送往天葬场由狼处理，施行天葬。这种“吃肉还肉”的风俗，体现了万物循环的朴素唯物主义观念，尊重大地母亲，肉身化在天地之中，死后也绝不破坏一块草皮。这种做法在历史上曾有效地保护了草原的自然生态。例如，书中的老牧民毕利格老人说：“难道草不是命？草原不是命？在蒙古草原，草和草原是大命，剩下的都是小命。小命要靠大命才能活命，连狼和人都是小命。”<br />
　　三、《狼图腾》一书中还以大量精妙的细节，描绘了狼在草原生物链中的巨大贡献：<br />
　　千百年来，牧民认为草原有四大害：草原鼠、野兔、旱獭和黄羊。草原鼠掏洞挖沙，是毁坏草场的第一大害。而狼是消灭草原鼠的主力，小狼要吃下无数草原鼠才能长大，小狼崽在成长中练习捕猎，就是从捕鼠开始的。而草原鼠和旱獭在冬季进入冬眠，野兔就成为狼在冬季的主食之一。蒙古草原从未发生过澳大利亚的那种兔灾，功劳主要在狼。旱獭的大量繁殖也毁坏草场，又深又暖和的獭洞是蚊子过冬的天然暖房，草原蚊灾使人畜无法生存，而狼捕杀旱獭，也为草原立功。黄羊群吃草就像打草机一样可怕，黄羊群经过之处，草场几乎被掠劫一空，而狼有效地控制了黄羊群的数量。还有草原上的马群也是破坏草原的罪魁。马群食草量惊人，蹄子刨草，马尿又会烧死大片的青草。而在原始游牧草原，狼每年会吃掉50%的马驹子，从而控制了马群自身的盲目繁殖。<br />
　　更为重要的是，《狼图腾》一书中，还记述了狼怎样“训导”草原人维护草原的“科学”方法。书中用大量的事实证明了狼具有高超的军事才能，它是草原骑兵的军事教官。几千年来，人数极少的草原人抗击了中原农耕文化对草原的侵蚀和掠夺，靠的就是从狼那里学来的卓越本领。因此，草原狼是草原生态链中关键的中心环节。《狼图腾》一书颠覆了以往人们对于“狼性”意味着贪婪残暴的肤浅认识，具有振聋发聩的作用。<br />
　　综上所述，《狼图腾》一书中所表述的草原“科学”的“发展”观，与今天的现实有着密切的关系，并有深刻的参考价值。科学要求平衡、要求可持续、要求遵从自然规律――这些都可以从狼在草原生态中的功能找到依据。而人类“发展”所必须具备的性格要素，恰恰就是“狼”的强悍进取、积极竞争的意识，是狼的自由独立品格和团队合作精神。那些充满奴性、羊性、家畜性、不思进取的落后民族，必然会被顽强勇猛刚毅的狼性民族所战胜。<br />
　　面对当下全球化背景下严酷的市场拼搏，面对人类的生存资源不断丧失与破坏，我们一度对“发展”这个词语心生畏惧与抵触，潜藏着对于“发展”等同于“破坏”的种种疑问和焦虑，就像我们一直都把“大灰狼”当作阴险狡诈残忍的代名词而陷入了羊性的误区。所谓的生态破坏难道是从全球化以后才开始的么？那些性格弱化软化的民族，由于落后而被挨打被掠夺被侵略造成的资源流失；中华民族所经历的数次农民起义、战争对生态的破坏；建国后一次次反科学的共产风、浮夸风、大跃进、阶级斗争、文化大革命等等，对经济建设、人文环境、自然生态的毁坏，难道还不足以说明：“不发展”所造成的共同贫穷，“不发展”所形成的文化封闭与思想停滞，对于人类的前景，同样会带来严重的后果。<br />
　　――长篇小说《狼图腾》，一扫当下文学作品中的脂粉之气萎靡之气，为我们注入了一股鲜活强健的狼性血液。“狼图腾”精神令人信服地破解了我们观念上的种种障碍，水乳交融地将“科学”与“发展”有机地结合在一起。世界已经进入了信息科学时代，以“狼道”顺应天道，华夏民族别无出路。<br />
<br />
<br />
　　（作者系黑龙江省作家协会副主席，本文为2004年4月“绿色中国”第三届论坛上的发言）</div>

							
							
							
							
