																											<div id="postmessage_54137" class="t_msgfont"><img src="http://www.wenyunchao.com/wp-content/uploads/2009/03/http_imgloadcgi.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
