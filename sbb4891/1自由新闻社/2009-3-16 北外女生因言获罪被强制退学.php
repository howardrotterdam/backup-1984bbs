先是诧异，后是沉思。本来自从有了90后这个概念，80后和90后貌似就是两个很对立的群体。80后讽刺90后脑残，90后嘲笑80后落伍。现在看来，90后作为正在成长的一代，还是有自己的思想的。我们国家的前途和未来是大有希望的。 <br />
<br />
教育部都在干吗？教育部忙着督促动员全体中国人民学习外语了。从小学，中学，再到大学，最后还有各种的成人培训。这还不算完，现在已经荼毒到了胎教和双语幼儿园。中国公民从还未出生起就正式踏入了学习外语的漫长艰辛道路。不学外语，上不了初中，考不上高中，更不让上大学，大学出来了没四六级证也找不到工作。不学外语，那么你就没有前途没有出路。不学外语，你就只能回家当农民种地去。因为招收新生、录用才俊、晋升职务全部不分青红皂白一律考核外语并作裁判依据。竟然是外语影响和决定了中国公民的一生，不知道这算不算是一种悲哀。 <br />
<br />
听说过霸王产业，那么教育部更像霸王部门。作为所谓中国最好的外语学院最好专业中的一员，我充分能够理解，中国人学习外语时的水深火热。它不是一种兴趣一种爱好，而是完全的强制。不管你未来想做什么，会不会用到外语，会用到多少，一律必须学。从幼时起就不得不背着这个沉重包袱。我们不反对学习，学习是好事，学习外语和国际化接轨更是好事，可我们能否有自己的安排和选择？它由不得国人选择。世界上没有哪个国家是推行外语必修制度的，而中国的教育部就是如此的霸王。 <br />
在霸王部门的强制下，家长督促，学校逼迫，顶着升学就业的压力，拿到一两张薄薄的证书，算是能够长出一口气，终于完成任务了。而事实上，绝大部分人过了所谓的四六级却还是没办法和外国人正常流利对话。这又不得不牵扯到一个很复杂的问题，中国的外语教育是完全的应试教育。应试的外语教学制度下，填鸭式教学方式下，国人学的是哑巴英语，连最基础的对话都做不到，不知道这样的学习又有何意义。拿着四级证书自娱自乐么？ <br />
我们是社会主义国家。马克思主义坚持各民族语言文字一律平等，反对强迫一个民族学习、使用另外一个民族的语言。可教育部推行的外语必修制度使中国公民因为不懂英语等外语而被剥夺受教育权、职称晋升权。我国宪法第4条规定：“各民族都有使用和发展自己的语言文字的自由。”我们可不可以说，在外语教育制度上，教育部既违背了马克思主义的原则，又没有遵守国家的宪法？ <br />
<br />
我国宪法第19条规定：“国家推广全国通用的普通话。”但在外语必修制度的作用下产生的现实却是：一个中国公民普通话水平不高影响不大，可以读高中，考大学，考硕士、博士；但一个中国公民外语水平不高则直接影响一生的教育和发展，就是读函授、自考本科，也有学位外语考试在卡住你，更不用说读硕士、博士了。由此可见，外语必修制度把外语的地位置于中国人的母语之上。语言本身是一种工具，同时又是一种文化，一种语言是一种文化的承载体，对于培育民族精神、孕育民族情结、弘扬民族文化都有极强的凝聚、教化作用。母语的衰落必将深刻地影响到民族文化的纯洁性和可持续发展能力。 <br />
一代又一代，教育部号召的外语学习不仅是国人沉重的包袱，更像是一种文化侵略。包包和阿紫提出的在国家层面上设置成人礼，其实是一种对民族传统文化需要回归的召唤。古时，男子成年要举行冠礼，女子成年要举行及笈礼。这始于周代，代表着从此可以作为成年人参加各种社会活动，承担着社会责任。有着浓郁的汉文化情节和几千年的悠久历史，更能让人明确自己的社会责任和增添民族自豪感。 <br />
<br />
作为一个中国人，作为一个懂三门外语被外语荼毒了二十年的中国人，我要说，我爱我们祖国的文字，我爱我们中华的文化。 <br />
<br />
我对外语教学的弊端深深的担忧，教育部都在忙什么？教育部，能不能不那么霸王？</div>

							
							
							
							
