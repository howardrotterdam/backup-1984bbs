女大学生说：“13日上午和下午，我们系的刘老师2次找我谈话，先是了解关于学生和找工作情况的，接下来就直接问我博客的事情，说我的博客直接攻击教育部，攻击教育部就是攻击国家政权，给学校领导造成了很大的压力。学校要求我立刻停止博客更新，撤掉文章，否则后果很严重。接着刘老师拿出了学生守则，说你在外面实习没有办注册手续，按照规定是旷课，已经连续2周了，所以你已经违反校规，达到了被除名的条件”。<br />
<br />
有这种政治素质过硬、形式逻辑混乱的”刘老师“，”北外“可以改名”北京外星人学校“了。而”北外“的做法――将写博客批评教育部的学生强制退学，已经制造出一起骇人听闻的当代文字狱，让我们只觉得”北外“所在非人间。<br />
<br />
如果女大学生所说属实，那么如下的描述或将并非小概率事件：无数网民直接攻击北外，攻击北外就是攻击教育部，攻击教育部就是攻击国家政权，给和谐社会造成了很大的压力，也让原本就声名狼藉的中国高等教育制度再创名声负增长之新高。</div>

							
							
							
							
