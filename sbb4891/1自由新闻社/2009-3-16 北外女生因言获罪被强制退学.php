当今的教育部绝对是扼杀人才的侩子手。应试教育，从小学初中起的外语必修制度,&quot;一刀切&quot;的高考制度,不让转系的大学教育制度等等，无疑都是对人材的一种压制和摧残. 严重阻碍、扭曲专业人才发展，破坏人才生态系统。社会对人才的需求是多元的，而人才的形成由于受天赋、兴趣、环境、经历等方面的影响，也是多元的，是各有特点的。这种多元人才之间的相互补充，适应着社会对人才的多元需求。而赵本山很注重发现人才,培养新人,挖掘新人的潜力,最主要的是找到合适的定位。他带红了小沈阳等一批土生土长的农民。赵老师文化是不高，也没有专家们一套一套的科学理论，但若赵老师做教育部长，至少能做到有教无类，因材施教，各尽其材。 <br />
