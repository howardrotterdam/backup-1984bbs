													<h2>北外女生因言获罪被和谐</h2>
						
						
																											<div id="postmessage_54168" class="t_msgfont"><a href="http://blog.qq.com/qzone/622008752/1237143424.htm#pageNavigator" target="_blank">http://blog.qq.com/qzone/622008752/1237143424.htm#pageNavigator</a><br />
妈妈：我被北外强制退学了。<br />
阅读(233219) 评论(4266) 发表时间：2009年03月16日 02:57 <br />
本文地址：<a href="http://qzone.qq.com/blog/622008752-1237143424" target="_blank">http://qzone.qq.com/blog/622008752-1237143424</a> <br />
<br />
本文标签: 香奈尔 北外 教育部 外语 退学<br />
本文已被推荐到腾讯博客, 点击了解更多精彩内容<br />
 <br />
 <br />
<br />
学校责令我的爸爸和妈妈必须今天赶到北京，因为他们的女儿闯下了大祸，他们在星期一当天必须到北京到学校，负责后果自负。 <br />
天亮的时候，妈妈就会赶到北京，爸爸已经来了，我百口莫辩。 <br />
我无法向爸爸和妈妈解释为什么是这样的。事情在突然间变的如此糟糕。 <br />
<br />
所以想了很久，博客还是继续更新把。既然我已经闯祸了，就继续下去。我真的不知道我错在那里？难道我很个人的表达观点会引来这么大的麻烦？ <br />
我真不知道为什么是这样的。也不知道继续写什么。 <br />
<br />
为了给妈妈解释清楚，刚才给妈妈写了一封信。准备明天交给她，面对她流泪的情形，我真不知道如何表达。只好用信，这个最传统的、比较正式的沟通方式。 <br />
我知道她为我这个事情流泪是必然的，可怜天下父母心。 <br />
<br />
今天就把这个信公开一下，也算为关心的朋友解释一下。无奈有时候也是一种勇气。因为有些事只有面对，我无法逃避。 <br />
<br />
<br />
<br />
亲爱的妈妈： <br />
<br />
在此，我十分惭愧的告诉您一件事。我被学校强行退学了。希望您听到这个消息的时候不要担心和难过。这一切都是我自己惹的祸，但你和爸爸、奶奶都要相信：你们的孩子是个好孩子。她没有做出什么不可饶恕的坏事，现在发生的一切都会用时间来证明：我没有错。如果说真话会成为不可原谅的罪过，那么我宁愿不要这个毕业证，我不想用失去尊严和说谎来交换一个基本没有价值的学历。 <br />
<br />
我长大了，不再是那个只会听你们安排的小女孩。 <br />
<br />
我觉的很有必要把这个事情发生的真实过程告诉您： <br />
3月4号,我在一个网站开了自己的个人博客，陆续写了些小文章。我上网时候知道最近在召开两会，很多人大代表和政协委员都在讨论关于教育改革和就业的问题，自己也就一时冲动，马上结合自己的感受写了几个小文章，内容主要是关于当今外语强制教育的,而且戏称让赵本山当教育部长，这句真是开玩笑的，因为是个人日记，就娱乐了一下。本来自己闹着玩的，没有想到被网站推荐到了首页，然后引起了网友的广泛讨论。3月12号,有报纸报道了这件事.该报道被其他多家报纸和媒体转载.报道的题目是这样的：北外女生炮轰外语强制教育。 <br />
<br />
接下来的事情就比较麻烦，13日上午和下午，我们系的刘老师2次找我谈话，先是了解关于学生和找工作情况的，接下来就直接问我博客的事情，说我的博客直接攻击教育部，攻击教育部就是攻击国家政权，给学校领导造成了很大的压力， <br />
学校要求我立刻停止博客更新，撤掉文章，否则后果很严重。接着刘老师拿出了学生守则，说你在外面实习没有办注册手续，按照规定是旷课，已经连续2周了，所以你已经违反校规，达到了被除名的条件。说实话，我再过三个月就毕业了，找到工作的很多学生和我一样都在外面实习，像我这样的情况也很多，为什么以前好好的没事，我一批评教育部就问题来了，今年就业形势这么紧张，谁不珍惜来之不易的工作？我恨不得天天加班讨好上司，表达我是有工作热情的，可以和男同学一样天天加班，不辞劳苦。 <br />
<br />
妈妈,你是知道的,我在公司实习,每天回来都很晚了,学校注册的地方是关了门的.我休息的时间,它也不开门.于是就拖了下来。接着，刘老师告诉我：你这已经没有注册两周了,这得按流程走.看你这心思也不在学习上,听说你一直写文章什么的,挺耽误学习.你在这方面有专长也挺好,发展发展.但你违反学校规定了,要是不尽快办手续,学校是不会保留你学籍的,连肄业证明也不会给你.到时候什么都没有就不大好了.接着她就给我纸和笔.催着我写退学申请.我当时一下就慌了.我很怕.其实近20年的学生生涯,我最怕的就是老师找谈话.虽然我是成年人了,但我对此有着习惯性的本能的畏惧.我翻着学生守则,开除和自愿退学是有区别的.开除是要通报批评,好象还要在教委备案的.不尽快退学,学校不给保留学籍和户口，那样我是不是就是黑户了? 我多年的书白读了? <br />
<br />
如果真是这样,你和爸爸该有多伤心? 我还有资格最你们的女儿么? <br />
我不想伤害你们,我当时脑中是一片空白.我问老师该怎么写,怎么个格式.老师说,写学习跟不上吧.我说,这也不实际啊.我学习还可以吧。于是,我含糊的写了工作原因,没能按时上课.这还算和学校列出的借口接近。 <br />
我很沉重的签下了我的名字. <br />
我相信老师和我一样清楚，强制逼迫我退学的原因是因为我写了博客。批评了教育部。而且老师也很明确的告诉我，如果博客不写了，承认错误了，一切都可以商量。 <br />
我不想在大庭广众之下违背自己的良心，已经批评了，就不改口了。我不想拿自己的尊严来和学校交换毕业证。 <br />
<br />
妈妈，我承认我冒然批评教育部的做法有点傻，有点自以为是。但我没有认为自己做这个事情有什么错误，现在也一样。这是我的真心话，我就是这样想的，无论到了什么时候，说过的话我一定会承认，做过的事情会自己担当。 <br />
<br />
我长大了，妈妈，我知道我做这个事情的后果。上大学这四年，尤其是找工作这半年，我对社会有了自己的认识。我算明白了一个道理：反正说假话你也找不到工作，说真话自己会过的舒服一点。 <br />
<br />
在我签字的时候,妈妈,我想到的不是我自己,而是你和爸爸. <br />
我知道你们一直为自己的女儿感到自豪和骄傲.其中有一个很大的原因：就是我从小到大都是一个乖孩子，学习也很好，在考大学的时候我有很多的选择：北大、北外或者其他学校都不是问题。但最后是你让我上的北外。你认为女孩学外语会很时尚，以后当个老师很安稳。我也就听了您的话，报考了北外最好的系之一：德语系。 <br />
<br />
但我如果不签这个申请,学校会不会还找出各种理由证明我是个坏学生,然后把我开除,不要再继续给学校捣乱抹黑?如果你们知道我连肄业证都拿不了,也许连学籍资料都没有了,你们该是什么样的反应?该是何等的伤心?那你和爸爸是不是会更加伤心,会想,你们的女儿怎么会是这样的呢? <br />
<br />
离开老师办公室的时候,脑中映出的,是妈妈，你的容颜. <br />
妈妈,我来到北京外国语大学,最高兴的应该是您.让女儿进外语学院学外语,是您的心愿.按照您的意思,我放弃了我最爱的北大中文系.来到最好的外语学院,最好的专业.终于圆了您一个外语的梦. <br />
<br />
我仍记得七岁的时候,您和爸爸就送我去学外语.东北的冬天很冷.爸爸骑自行车载着我去上课。我仍记得,从小学到高中,你们对我寄予厚望,让我一直在最好的重点学校读书.您一直监督我的功课,三四岁的时候就教我认世界地图,记历史朝代.您自己省钱,却买大量的课外书给我,从儿时起的积淀至今都让我收益菲浅.原本学俄语的您,为了我艰难而努力的学起了英语,每天考我单词,听我背课文.我的每点进步都让你们激动不已,我的每个个挫折都让你们无比焦心.高考成绩出来后的电视台采访我,让我谈学校心得，在旁边坐着的您,露出我见到过的，最美丽的笑容. <br />
<br />
二十几年过去,你们的女儿,在你们细心的培养和呵护下,长大了.而你们,却老了. <br />
在爸爸五十岁生日的那天,我很刹风景的泪流满面.因为我没想到,像钢铁一样的父亲,曾经为我撑起一片天的你们,也会老. <br />
流泪的那一刻,我知道我长大了. <br />
长大,意味着我要承担做为一个成年人的责任.意味着,我不该再让你们为我操心.意味着,今后的几十年,轮到我,作为女儿,来悉心的照顾你们. <br />
而今天的事情,让我很羞愧.我很怕.很怕伤害到你们. <br />
<br />
。。。。。。 <br />
可是现在，可我所说的,让我和您引以为荣的北京外国语大学,却在以我为耻. <br />
妈妈,从小你和爸爸就教我一个做人最基本的道理,就是诚实,要说真话. <br />
我认为自己一直是个活的很真实的人.我遵从自己内心最真实的感受,表达内心最真实的想法.难道在中国,不会说外语的人是没有活路的,说真话的人是可耻的么? <br />
我真的认为,外语强制教育制度是不对的. <br />
<br />
爸爸也是个党员.作为一个中国人,我真的无比热爱我的国家.我丝毫没有反叛国家的意思.受教育,是每个公民的权利和义务.在受教育的机会上,应该每个人都是平等的.事情都是客观的.我只是从很善良的角度提出了我的看法.我想我们的教育制度变得更好.人们的生活更加的幸福.我是为了国家更好.国家强大了我们才更加有地位. <br />
只是我有点天真，以为自己读了几年书，看了几本书就以为自己很了不起，就以为说真话，提建议是一种提醒公民价值的行为。 <br />
所以很傻天真，很天真的人是我。对不起，妈妈。我错了。 <br />
在这个世界上，我不知道有些话是不能说的。 <br />
<br />
但是，妈妈,爸爸,请你们不要为你们的女儿担心.这件事情,不会改变我的人生.请你们坚信你们的女儿是优秀的,这件事并不会代表什么.我会继续做一个说真话的人.继续为我自己的未来而努力的。 <br />
我一定会让你们继续骄傲的，像以往一样，让你们充满自豪，为我。</div>

							
							
							
							
