													<h2>外语剥夺了中国人的受教育权</h2>
						
						
																											<div id="postmessage_54140" class="t_msgfont">香奈尔&nbsp; &nbsp; 发表于2009年03月12日 01:03 阅读(121781) 评论(2529) <br />
分类： 个人日记 举报 <br />
 <br />
 <br />
<br />
我认为，外语剥夺了我们受教育的权利.<br />
查了一下,在法律上,受教育权的解释是这样的.<br />
<br />
受教育权，是我国宪法赋予公民的一项基本权利，也是公民享受其他文化教育的前提和基础。所谓受教育权，是指公民享有从国家接受文化教育的机会和获得受教育的物质帮助的权利。受教育权包括两个基本要素：一是公民均有上学接受教育的权利；二是国家提供教育设施，培养教师，为公民受教育创造必要机会和物质条件。<br />
可外语却剥夺了我们受教育的权利.<br />
外语，一个其他民族的语言，却是中国人是升学的一道门坎.我们从小学到大学甚至读研究生和博士,唯一一门一直要学的科目就是,外语. 考外语，从幼时一直考到人近中年.<br />
外语是必修必考的.有太多中国人,因为不懂外语,而被剥夺读中学、大学和硕士博士研究生等受教育的权力.外语水平不高直接影响中国人一生的教育和发展.<br />
<br />
