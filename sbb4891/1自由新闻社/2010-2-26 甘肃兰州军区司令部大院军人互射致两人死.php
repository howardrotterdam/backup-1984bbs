&nbsp; &nbsp; 那年初冬,天气奇冷.１１月８日零时许，湖北省襄樊市某部战士邵江彬、耿学杰用脚踹开排长房门，用牛耳尖刀将排长刺死，抢走排长身上带的部队兵器室钥匙，盗走两支折叠式冲锋枪和１１００余发子弹，逃入深山消失踪迹．据后来了解：那邵江兵是劳改释放人员，因强奸罪被判３年劳改．出狱后因当时我兵役制度不完善，家里走后门送礼将其送入部队，此人在部队里和认为当猪馆亏待自己的耿学杰臭味相投惺惺相惜，两人就有盗枪后潜逃作大案的想法．邵江兵军事素质过硬，枪法奇准．还曾在总队组织的射击比赛中得过亚军．两人在部队中拉帮结伙打架斗殴，被部队领导多次批评，两人心怀不满最终于走上了杀人抢枪负罪潜逃的罪恶道路。 <br />
<br />
　　　　两个穷凶极恶的罪犯从湖北逃窜往四川的路上，抢钱夺车连杀数人．其手段之残忍，一般在抢了钱后绝不留活口，一枪毙命或勒死．在路上抢夺摩托车，就直接对着驶过的车子扫射，将驾车人击毙后夺车而去．两个家伙在逃到四川省乐山市境内后，他们将走到他们罪恶人生的尽头．由于连日奔逃，两人疲惫不堪蓬头垢面．有旅店又不敢去住，只好露宿在田间老百姓打了麦子留下的麦杆堆里．１１月２７号．天还没亮，一个农民去田间巡视自己的农作物，见麦草堆里有人在睡觉．联想到近日偷鸡的在村子里偷得大家心慌．他连忙跑去村长那里报告，说是可能有偷鸡贼．村长但学清连忙带了几个人去现场．一见两人浑身脏的要命一口外地话不由生凝．将两人强行扭送乡政府．两人虽然不情愿，但一看周围围观的人多．只好随大家一起来到乡政府的治安室。 <br />
 <br />
　　　　治安室里，住乡民警，乡治安员，乡武装部长和村长等人问其是从何地何时来此，干什么？两人吱吱唔唔的说不清．于是准备检查他们提的黑包包．两逃犯拒不接受检查，说是里边装的收来的天麻（一种草药）正在争执中，耿学杰突然弯腰拉开提包拉链，当过兵的乡治安员突然看见里边有一枝黑黑的枪管，枪！他们有枪！他刚刚喊出这一声．邵江兵，这个受过严格训练的家伙突然施开拳脚将围在身边的人打翻，一串子弹从耿学杰的枪中射出，开枪时，乡治安员猛扑上去抢夺他手上的枪，子弹打中但学清的手臂，又当场击中路过窗外的当地一个盐厂的厂长头部，并打伤两个村民．此时邵江兵掏出了一把刀疯狂的乱挥，住乡民警和武装部长身负重伤，但仍拼死与其博斗，使其没有时间拿出另一只枪．而但村长和乡治安员也不顾一切的拼死抢夺耿学杰的枪．疯狂的耿学杰猛扣板机，子弹打光了但奇迹般里边一个人都没伤到．乡治安员猛的一拳再加一膝盖将身材瘦小的耿学杰打翻在地，抢过那只打完子弹的枪拖起手上打飞一大块肉的但村长跑出门去．另两个身负重伤的住乡民警和乡武装部长也退出了房间．那抢走的一只枪大大的刺激了两个罪犯，邵江兵用仅有的那只枪开路，耿学杰挥舞刀子．两人飞快的冲出了乡政府．而身负重伤的四个人提着空枪只能眼睁睁的看着两个罪犯跑掉．此时乡干部闻讯追了出来，两个罪犯拦下辆白泉农用车挤上去威胁开车，车刚一起动，乡干部赵林元赶来，还没开口被邵江兵举枪打倒．车刚开出十多里，由于是重车驾驶员心慌一下死火了．有个过路的热心的来问：怎么了？也被邵江兵打死．两个罪犯马上下车又拦了辆野马车（四川十年代自产的一种越野车）疯狂的逃跑，路上还丧心病狂的向路上的行人开火，打伤数人．当车到井研高凤乡时，遇到民兵和群众的拦截．邵江兵疯了样对着老百姓开枪，十多人被打死打伤，有个抱在怀里的小孩也被打断手指．他们的车从血染的路上仓惶而过，见路上是实在不能跑脱的，两人弃车钻进了白岩沟里． 白岩沟，约两公里长，怪石散落沟里，一道山涧小河淙淙流淌，易守难攻，敌暗我明，乐山市政法领导小组组长担任总指挥，决定搜索前进，就地击毙。 <br />
