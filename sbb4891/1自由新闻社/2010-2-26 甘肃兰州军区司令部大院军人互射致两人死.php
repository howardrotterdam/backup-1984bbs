<br />
　　 白岩沟围剿战斗纵跨三天两夜，长达６０个小时，参战公安民警、武警６００余人，解放军官兵１２９人，动用了曾参加越战的解放军某师的侦察连、防化连、喷火连，各类参战人员（包括当地民兵）总计１５１６人。战斗中出动大小车辆１２０台，使用子弹１７０００发，手榴弹３３４枚，四零火箭弹２０枚，火焰喷射器３具，催泪弹１３枚，毒气弹９枚，我方付出了牺牲8人(含１名围观群众)、伤9人(含3名围观群众)的惨重代价。<br />
