By SHARON LaFRANIERE<br />
Published: March 14, 2010<br />
<br />
BEIJING ― The Chinese authorities have warned major partners of Google’s China-based search engine that they must comply with censorship laws even if Google does not, an industry expert with knowledge of the notice said Sunday. <br />
北京 - 一位行业专家周日说，中国当局对谷歌的主要合作伙伴提出了警告：即使谷歌不服从内容过滤的法规，他们也必须服从。<br />
<br />
The Chinese government information authorities warned some of Google’s biggest Web partners on Friday that they should prepare backup plans in case Google ceases censoring the results of searches on its local Chinese-language search engine, said the expert, who did not want to be identified for fear of retaliation by the government. <br />
<br />
The warning was the latest indication that two months of negotiations between Chinese officials and Google over government censorship have reached an impasse, making it more likely that Google will end up shutting down its Chinese search engine. The two sides have been at a standoff since Google announced in January that it planned to stop self-censoring the results of searches on its Chinese site, google.cn, in reaction to what it described as China-based cyberattacks on its databases and e-mail accounts. <br />
<br />
The warning was intended to head off a wave of frustrated users should their Internet searches be stymied because of Google’s conflict with the government. Google controls nearly 30 percent of China’s Internet search market. <br />
<br />
China’s most popular Web portal, <a href="http://www.sina.com.cn" target="_blank">www.sina.com.cn</a>, features the Google search box in the middle of its home page. Ganji.com, another highly popular Web site, displays Google’s search box in its upper-left-hand corner. <br />
<br />
Google, however, is unlikely to stop censoring its results, people with knowledge of the situation said. Instead, they said, it is more likely that the company will shut down the Chinese search engine and try to reach Chinese customers through its search engine based in the United States. <br />
<br />
If it does close its Chinese search engine, Google has other operations in China that it hopes to save, including a toehold in the country’s mobile phone business. <br />
<br />
Eric Schmidt, Google’s chief executive, said last week that “something will happen soon” to resolve Google’s fate in China. Reporters have been camped at Google’s Beijing headquarters since then in anticipation of an announcement that the company will close down some or all of its China operations. <br />
<br />
Since Google opened the China-based service about four years ago, it has filtered responses to users’ searches to remove results that the government finds objectionable, including pornography and content on political topics like Chinese human rights issues. Despite the self-censorship, the company has drawn a strong following, especially among educated and wealthier Chinese Internet users. <br />
<br />
Google has a widespread network of Chinese partners that have set up their Web sites to link to Google’s Chinese-language search engine. The government’s warning was a reminder to operators that they are responsible for any content on their sites, even if it is provided by a third party like Google. Those companies could switch to services that are more accommodating to the government, like Baidu, the search engine that holds the dominant share inside China. <br />
<br />
Should they remain loyal to Google, the companies could satisfy government censors by filtering their customers’ searches themselves, excluding objectionable topics before relaying them to Google. But that option could prove difficult, especially for smaller companies, which would have to buy or develop software to do that job. It would be easier for most simply to switch to another search engine. <br />
<br />
If Google refuses to censor its searches, industry specialists said, the government will most likely disrupt its service temporarily, frustrating users and driving them away from the Google search engine and possibly from its partners’ Web sites. <br />
<br />
Users of Google’s worldwide search engine, google.com, would be likely to find their situation unchanged, industry specialists said. The site is accessible in China, but Chinese Internet users can gain access only to Web pages that have been approved by Chinese censors, rather than Google’s own employees. <br />
<br />
Asked Sunday about the Chinese government’s warning to Google partners, a Google spokeswoman, Courtney Hohne, declined to comment. A company statement said last week that Google had “been very clear that we are no longer going to self-censor our search results.” <br />
<br />
“We are in active discussions with the Chinese government but we are not going to engage in a running commentary about those conversations,” the statement said. <br />
<br />
China’s position has seemed equally unyielding. On Friday, Li Yizhong, China’s minister of industry and information technology, warned Google, “If you want to do something that disobeys Chinese law and regulations, you are unfriendly, you are irresponsible and you will have to bear the consequences.” <br />
<br />
<br />
