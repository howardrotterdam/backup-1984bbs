													<h2>陈冠希：修电脑很后悔 和谢霆锋永远是朋友</h2>
						
						
																											<div id="postmessage_406020" class="t_msgfont">华商网-华商报12月30日报道 “我不相信我是个坏人，我不是个坏人”、“我吸取了教训，但我到现在也不明白，为什么我是那个坏人”……因“艳照门”事件隐身许久的陈冠希，日前接受《GQ》杂志专访，打开心扉聊人生种种，而再提及一年前的“那件事”，陈冠希道出心底话：“把电脑拿去修，这也是我活这么大最后悔的一件事。”<br />
<br />
■最后悔的事把电脑拿去修<br />
<br />
“把电脑拿去修，这也是我活这么大，最后悔的一件事。”陈冠希坦言，“艳照门”事件对于他来说，是个教训，但同时，心中一直有个疑问深藏于心，“为什么我是那个坏人？”至今他也没闹明白，就算开了记者会道了歉。“整个事情闹这么大，一定要一个人说，这就是那个坏人，然后事情才可以平息。好，没问题，我就来当那个坏人。可我真是个坏人吗？我不是，人们只是需要一个替罪羊……坏人做坏事，我没有做坏事。”陈冠希有些愤懑，“我去年损失了很多钱！我甚至离开了我的家！我是那个坏人？”<br />
<br />
提及“艳照门”事件，陈冠希用的名词是“那件事”，在他看来，“那件事并不是一个性事，而是私事，是偷窃。对其他人说，是性事。但对我，是对我隐私的侵犯。”<br />
<br />
性、爱、钱、家庭，在他心里的排序是这样的：“爱和家庭一样，之后是钱，最后是性”，“我是男人，这个世界男人喜欢性，也需要爱。我不能说，在那件事之前我喜欢性胜过爱情，现在我更相信爱情不怎么喜欢性了，我不能这样说。”<br />
<br />
■最大的财富没有变疯，没有自杀<br />
<br />
如今，回想起事件发生后那段日子，陈冠希说最艰难时，是2008年的情人节，“不敢去玩，不敢去吃东西，什么都不敢，两三个礼拜都没有离开家。”后来事情的发展出乎他所料，不知道会到什么地步。<br />
<br />
在人生最低谷，他没有选择极端方式，“我觉得到目前为止，我人生的最大财富就是没有变疯，没有自杀。”陈冠希说，因为想通了，“我的生命结束了吗？没有。我的生活还在继续。”<br />
<br />
离开香港，离开娱乐圈，回归平常人生活，陈冠希自言反而学到很多――“做个人，做个普通人，真好。”他学会熨衣服，以前则是扯着嗓子呼喝工人：三分钟就要熨好！但现在他自己熨衣服，发现原来三分钟是熨不好的。“原来我当时真是坏人。”<br />
<br />
<br />
陈冠希强调，艳照事件爆发后，女友从第一天到现在都陪在身边，“我觉得她是Superwoman（女超人），她一直都支持我，如果没有她的支持，我不知道敢不敢回来香港做那个记者会。”<br />
<br />
■和谢霆锋永远是朋友<br />
<br />
由于张柏芝的关系，陈冠希与谢霆锋之间，霎时变得很尴尬，但陈冠希称两人永远都是朋友。“我们在香港国际学校的时候，非常熟……如果他需要什么帮忙，我一定会帮。因为，就算在生命中的最后一天，他也是我的朋友。因为我们生命中最纯洁的时光，就是我们年轻的时候，我牢记这些，我永远都不会忘记。”<br />
