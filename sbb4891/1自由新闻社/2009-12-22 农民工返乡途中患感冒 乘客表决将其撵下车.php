当白口罩罩住民工陈国芳和张大有的嘴后，他们从广西乘大巴的回乡路发生戏剧性转变：司机及全车乘客误以为他们患严重甲流感，遂在服务区被活生生撵下车“隔离”……昨日凌晨4时许，执法队员发现，两人正摸黑顺高速公路往家赶。<br />
<br />
空调出故障路上感冒了<br />
<br />
陈国芳和张大有是四川内江人，在广西一家工厂打工。3天前，工厂提前完成今年生产计划，遂放假让工人提前回家过新年。<br />
<br />
哪知，回乡的大巴在行驶中空调发生故障，没有暖气让所有乘客感到寒冷无比。前天早晨，陈国芳和张大有醒来时，不约而同出现嗓子沙哑还伴轻微咳嗽。每次咳嗽，其他乘客都投来异样目光。其间，有乘客好奇问，咋两个人一起咳嗽。<br />
<br />
陈和张先后解释，他们在同一家工厂打工，提前回家过新年。不说还没关系，一讲车厢内就炸开锅。距元旦还有近10天，你们恁早回家不合常理！其他人都不感冒，咋就你们两个咳嗽？……乘客把一个接一个疑惑抛来，陈和张委屈得无言以辩。<br />
<br />
怕影响他人戴上白口罩<br />
<br />
昨上午，大巴驶进贵州境内一处高速公路服务区。陈国芳和张大有商量后，在商店各买1个白口罩。按他们想法，戴口罩后，咳嗽声音会小很多，既可避免感冒传染其他乘客，还不影响别人休息。<br />
<br />
大巴继续行驶。陈国芳和张大有戴白口罩的初衷虽好，却没任何人买账：投来异样目光和询问的人更多。让两人很伤心的是，前排两个乘客完全像避瘟疫一样留下空座位，情愿到车厢最后面的过道也不返回坐椅，且满眼惊恐地往他们看。<br />
<br />
“你们是不是得了甲流感？不然啷个戴口罩？”有乘客终于忍不住问。陈国芳和张大有涨红脸：连续2天的解释，已让他们不知该如何回应才能让其他乘客满意。<br />
<br />
最终，两人选择沉默。但几个性急乘客先后跑到驾驶室告诉司机说：“有两个患严重甲流感，要是全车人被感染，你当司机的脱不了爪爪。”<br />
<br />
“民意表决”撵下车隔离<br />
<br />
昨日凌晨，大巴驶入渝黔高速公路綦江段服务区。全车近30个乘客都在激烈讨论，迫于乘客压力的司机听取大多数乘客意见后，打开车门。<br />
<br />
原来，其他乘客特意叫司机把大巴停在服务区，然后进行了“民意表决”，方式是同意让陈国芳和张大有下车“隔离”的举手。让两人心寒的是，所有乘客举手同意让两人马上下车到服务区去“隔离”甲流感。<br />
<br />
最初，两人以半夜三更下车太冷为由，不愿下车。乘客们便你一言我一语地说，服务区有医生还有特效药卖，下车吃几颗就好了。两人知道这种说法是假的，坚决不下车。<br />
<br />
让两人终身难忘的一幕发生了：一个身材高大、很强壮的男乘客走来，像拎小鸡般抓起虚弱的陈国芳往过道拖，另两个男乘客见状，也凑过来把张大有同样往过道拉。<br />
<br />
七八分钟后，两人被撵下了大巴。服务区哪有特效药！两人只有凭感觉，顶着寒风往重庆主城方向走，骂声不断。<br />
<br />
经检查只是患普通感冒<br />
<br />
昨日凌晨4时许，市高速公路执法支队六大队执法队员，发现在高速路上摸黑行走的陈国芳张大有二人。见到执法队员瞬间，两人如见救星：眼泪汪汪地讲述回乡途中的委曲……很快，两人被请上开着暖气的执法车，前往綦江县人民医院发热门诊。经医生检查，两人患的只是普通感冒。<br />
