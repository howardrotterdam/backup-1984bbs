																											<div id="postmessage_423275" class="t_msgfont"><a href="http://news.xinmin.cn/rollnews/2010/01/09/3298685.html" target="_blank">http://news.xinmin.cn/rollnews/2010/01/09/3298685.html</a><br />
《图书公平交易规则》出台新书网售不低于八五折<br />
2010-01-09 09:25 来源：文汇报 作者：陈熙涵 共 0 条评论 关键字：规则,图书市场,图书价格<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;新年伊始，一个具有里程碑意义的行规正式在我国出版业推行。书店给出版社结算周期不能超半年《图书公平交易规则》的第二大要点在于对结算还款环节作出了具体规定。<br />
　　东方网1月9日消息：新年伊始，一个具有里程碑意义的行规正式在我国出版业推行。记者今天获悉，酝酿已久的《图书公平交易规则》通过国家相关部门的审议、修改后，于北京图书订货会开幕当天正式出台，国家新闻出版总署副署长阎晓宏、国家版协主席于友先出席。<br />
<br />
　　行业规范是否只是“纸老虎”<br />
<br />
　　一套《中国通史》，定价1200元，实际售价100元；680元的《资治通鉴(全18卷)》售价70元……如今的图书市场，一套一二千元的书在打折的时候卖几十元已不是新鲜事。随着图书折扣战日渐升级，实体书店与实体书店、国有书店与民营书店、实体书店与网上书店之间恶性竞争“战火连燃”积怨日深……不少书店一边抱怨，一边狠狠地将书的折扣压得更低。更要命的是，消费者形成了一种意识，好像书不打折是不正常的。今天推出的《图书公平交易规则》中，对图书促销环节作出了明确规定：不得低价倾销新书，不得进行任何形式的低价（低于图书正常出版成本价）竞争和竞标。对出版一年内的新书（以版权页出版时间为准），进入零售市场时，须按图书标定实价销售，网上书店或会员制销售时，最多享受不低于8.5折的优惠幅度。<br />
<br />
　　事实上，这则规定在执行上难度颇大。条例推出首日，记者登上国内最著名的网上书店“当当网”和“卓越亚马逊”，在首页上即能看到，7折、6.5折的新书还是不少，一个网站甚至打出“限时抢购秒杀”的宣传字样。在采访中，一些本应只做零售的网上书店道出了苦衷，他们认为大型网上书店的销售量已经远远超过一些年批发只有500万D1000万的批发企业，给网上书店的供货折扣如果和其他企业一视同仁，有不符合市场规律之嫌。<br />
<br />
　　据了解，虽然国外的读者俱乐部销售的打折书都是专门的俱乐部版图书，即发行2年以上的图书，新书不打折已成规矩。但是近日亚马逊、沃尔玛以及Target公司将精装书卖到了9美元一本的低价，掀起了在线图书价格战，这在一定程度上触及了行业规范的底线。<br />
<br />
　　一般来说，畅销作家的精装书要卖到25-35美元一本。而零售大鳄们之所以敢低于成本价促销，正是应了那句老话&quot;赔本赚吆喝&quot;---图书销量的日渐萎靡，让市场杠杆几乎要靠触底的方式来求得平衡。还有人提出，原来消费者花7折的钱从网上书店买书，还可以享受送货上门，他们已经形成了这种消费模式，现在如果突然变了，消费者需要一个适应的过程。书店给出版社结算周期不能超半年《图书公平交易规则》的第二大要点在于对结算还款环节作出了具体规定。长期以来拖欠货款成为中国出版业的顽疾。出版社一致反映，目前图书销售几乎所有的风险都由出版社承担，出版社将书发到书店，书店卖不掉可以无条件将书退回出版社，即使书卖出去了，还款也常常有账无期。3个月到账几乎成了&quot;传说&quot;，一年到账实属正常。有些经销商甚至恶意欠款。如今，《规则》对结算环节作出了具体规定，写明供货商与经销商之间的结算周期不能超过180天（半年），开具增值税发票后不超过90天。对此，北京人天书店有限公司总经理施春生提出，这些年来，书店的人工、房租、税收都在增加，而图书价格的增长却十分缓慢。反观美国有些州对文化产业实行零税收，欧洲一些国家对文化产业只征收5%-6%的税收，而我国出版企业的增值税则高达13%，很多书店难以维持经营。<br />
<br />
