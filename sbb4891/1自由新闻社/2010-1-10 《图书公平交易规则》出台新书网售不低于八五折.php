																											<div id="postmessage_423747" class="t_msgfont">新年伊始，一个具有里程碑意义的行规正式在我国出版业推行。记者今天获悉，酝酿已久的《图书公平交易规则》通过国家相关部门的审议、修改后，于北京图书订货会开幕当天正式出台，国家新闻出版总署副署长阎晓宏、国家版协主席于友先出席。<br />
<br />
在1月8日开幕的2009北京图书订货会上,国内图书出版发行业的第一部行业规范――《图书公平交易规则》正式发布。有关人士认为,《规则》的发布结束了图书交易缺少行业规范的局面,在我国图书出版发行业历史上具有“里程碑”意义。<br />
<br />
《规则》由中国出版工作者协会、中国书刊发行业协会、中国新华书店协会在新闻出版总署的主导下,经数年酝酿而制定。《规则》共九章三十条,从订货、供货、退货、促销、结算等环节对图书交易行为进行了全面规范。<br />
<br />
《图书公平交易规则》中，对图书促销环节作出了明确规定：不得低价倾销新书，不得进行任何形式的低价(低于图书正常出版成本价)竞争和竞标。对出版一年内的新书(以版权页出版时间为准)，进入零售市场时，须按图书标定实价销售，网上书店或会员制销售时，最多享受不低于8.5折的优惠幅度。<br />
