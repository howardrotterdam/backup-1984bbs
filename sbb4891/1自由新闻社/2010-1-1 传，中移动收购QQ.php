													<h2>盛传购腾讯 中移急否认</h2>
						
						
																											<div id="postmessage_411057" class="t_msgfont">【明报专讯】<br />
<br />
北京信息科技网站Donews昨天引述电信业内人士说，09年最后一天，中移动(0941)董事长王建宙到访腾讯(0700)总部，磋商中移动收购腾讯事宜。中移动发言人昨天随即向新闻界发出短信，「指内地个别媒体传言中移动洽谈收购腾讯的消息，毫无根据，中移动没有收购腾讯的意向。」<br />
<br />
引发中移动收购腾讯的揣测，是源于腾讯旗下的腾讯新闻网，在12月31日图文并茂地刊登了王建宙到访腾讯总部的报道。中移动发言人说，王建宙到访过深圳多家企业，腾讯只是其中一家。<br />
<br />
中移发言人：同时访问多间企业<br />
<br />
Donews则引述消息士透露，中移动收购腾讯公司的想法由来已久，从王晓初(现任中电信(0728)董事长)在任时就接触过几次，但因为中移动体制问题和腾讯对收购条件不满意而一直未有结果。随?中移动飞信被强制向中电信及联通(0762)开放互通，以及中移动在进军互联网进程中的诸多不顺，刺激了中移动通过收购腾讯以加快进军互联网市场的步伐。<br />
<br />
据腾讯新闻网报道，腾讯总裁刘炽平接待王建宙时，向王介绍了公司发展历程、业务布局、商业模式及社会责任等方面的工作。腾讯执行副总裁刘成敏展示了腾讯在无线互联网领域的应用及新产品设计、研发过程。报道指出，「王建宙对腾讯的产品设计、运营过程中表现出来的创新理念表示了肯定。」<br />
目前，腾讯的市值已达到3000亿元，拥有4.85亿个客户(表)。而中移动作为全球最多客户的移动通信运营商，正面临同业激烈竞争。数据显示，截至09年6月底，中移动手头现金及银行存款，共有2560亿元人民币。<br />
腾讯管理层近年不断减持<br />
<br />
腾讯过去一直深受投资好手的青睐，早在99年底，本港电盈(0008)主席李泽楷也曾斥资860万元，取得腾讯两成权益，成为其最大股东之一。2001年，李泽楷全部沽出股权，当时获利逾10倍，但现在看来只是九牛一毛。相形之下，有近百年投资历史的非洲最大媒体公司Naspers更有恒心，收获也就愈大。Naspers在2001年已经投资腾讯，一直很少减持，Naspers现在的持股超过34％，成为腾讯第一大股东(图)。<br />
反观腾讯一?执行董事近期减持现象却十分严重。腾讯的创办人及管理层，近年不断减持腾讯。主席马化腾在过去一年减持了300万股(表)，持股量已降至11.54%。据港交所的数据显示，总裁刘炽平在12月28日及30日，分别减持4万股及6万股，各套现652万元及982万元。他在12月中已减持10万股。<br />
<br />
明报记者王融融报道</div>

							
							
							
							
