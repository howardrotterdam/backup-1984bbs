													<h2>出版审查反暴力标准：三国斩将不能见首级落地</h2>
						
						
																											<div id="postmessage_213341" class="t_msgfont">出版管理反暴力标准:三国斩将不能见首级落地<br />
<a href="http://www.cnbeta.com/articles/91767.htm" target="_blank">http://www.cnbeta.com/articles/91767.htm</a><br />
暴力的标准，是逐渐变严格的，比如以前的连续剧《三国演义》中，人头落地很常见，但是现在就不行了<br />
大型网游《魔兽世界》，在停止运行50多天后，于数周前再次上线内测。在停止运行期间，新闻出版总署对上海网之易公司重新申请引进《魔兽世界》的出版申请进行了审批，并对内容进行了审查。<br />
<br />
上线内测当日，大陆五区密林游侠Rahx惊讶地发现，游戏中跟血、骨头有关的形象全被替换了，骷髅头像也不见了。所有由头颅标识的能力图示变成了抽象、神秘的盒子、箱子。 <br />
<br />
　　这些修改，资历较浅的玩家觉得新鲜有趣，而一些老玩家们却有些不习惯。Rahx举例道：“之前‘术士的腐蚀术’图示，是一个类似孢子的图示，暗 示了其生物特性，可是如今却变成了一个盒子；‘妖术之颅’，以前用巨魔的头作为标志，现在却被改成了一只箱子。”他觉得这样修改后，似乎失去了一些游戏的 概念和逻辑，“太抽象了”。在网上，有人称新的《魔兽世界》为“盒子世界”。<br />
<br />
　　在游戏的打斗过程中，画面上不会再喷涌出鲜红的血液。“如果说2007年那次修改是不见骨头，那么这次修改就连血也见不到了。”Rahx说。<br />
<br />
　　在经过新闻出版总署的审批准入之后，那些表现身体肢解、暗示生物组织截断分离的令人产生恐怖感的图案，全都被去掉。连游戏中叙述性的文字也作了细致的修改，一些含有挑逗性、令人产生暴力刺激联想的词汇消失了，用Rahx的话来说，变得“文绉绉的”。<br />
<br />
　　在《魔兽世界》修改的同时，最近文化部也查处了一批涉及“黑帮”主题的网络游戏。其中包括千橡集团旗下的SNS网站均牵涉其中，它们被责令删除网页中“古惑仔”、“江湖”、“教父”等插件。<br />
<br />
　　作为在年轻人中盛行的网络游戏，官方如何对其进行准入审批及日常监控？近日，文化部和新闻出版总署的相关部门，向本刊记者详解了其措施以及背后的深层考虑。<br />
<br />
　　溅出血来是不行的<br />
<br />
　　新闻出版总署相关人士对本刊记者表示，网络游戏具有出版物的所有特征，是具有意识形态属性的特殊商品，并非一般的文化娱乐产品。<br />
<br />
　　为了保证网络游戏出版物内容的健康安全，国家新闻出版总署按审批程序，组织游戏内容审查专家进行审查。截至目前，国家新闻出版总署已审批引进 200多种进口网络游戏，对其中170多种网络游戏中涉及国家安全、外交事务、民族关系、血腥暴力、淫秽色情、含有不良诱导游戏功能等共2500多处内 容，作出了删除、责其修改的处理。<br />
<br />
　　具体到《魔兽世界》，新闻出版总署负责进口游戏引入的科技与数字出版司网络出版管理处处长宋建新说，内容审查中，专家们发现重新申请引进的《魔兽世界》游戏中有暴力、血腥、恐怖等不健康内容，主要表现在两个方面：<br />
<br />
　　其一，含有暴力、恐怖、残酷等妨碍未成年人身心健康的内容。如，个别任务设置中有要求玩家砍头提首或取心提交，或要求将人头插在长矛上在指定地 点示众后才算完成任务；有非情节需要的曝尸、叉尸等残忍、恐怖场景；在表现玩家使用兵器攻击“人型生物”时，渲染鲜血喷溅效果等。<br />
<br />
　　其二，含有详细描述可致死、致残的细菌“武器”制作过程，易诱发未成年人模仿违反社会公德行为和违法犯罪的行为。如“皇家药剂师学会”发布的 “新的瘟疫”、“受难药剂”、“痛苦药剂”、“苦楚药剂”，涉及教授玩家如何对瘟疫病菌进行“样本提取”，然后“提炼病毒”、“活体实验（人体）”、“暗 中下毒”、“大规模散播”等。<br />
<br />
　　完全西化的东西必须修改<br />
<br />
　　《魔兽世界》从6月中下旬提交审批，7月中旬新闻出版总署将审查中发现的问题及修改意见回复给了申报单位。将近一个月的时间，很多玩家觉得这段时间过长。<br />
<br />
　　对此，宋建新回应说，这其实是按照程序来的，按照行政许可法，有20个工作日的审批时间，以及60个工作日对内容审查的时间。对所有进口游戏都 是这样，《魔兽世界》也不例外。申报单位如果把游戏内容处理好后申报，审查的时间会快些。送审的游戏，经总署审查还存在内容问题，就退回申报单位修改。这 个修改过程是不算在审批时限里的。<br />
<br />
　　他说，申报单位的修改有时需要好几个回合，这些使得玩家觉得时间比较长。这是因为申报单位修改内容，需要一个理解的过程。“到底什么叫做暴力？ 是一拳打出血来算暴力，还是一拳打出坑来算暴力？‘打’这个词，作为一个暴力词语，在游戏中是需要形象来展示的。上海网之易没有游戏的源代码，完成修改需 要美国公司的程序员来完成，申报单位要让美方对修改要求理解到位是不容易的。”宋建新说，“从这一点看，原创能力至关重要，否则，中国公司就只能受制于 人。”<br />
<br />
　　在重新审批《魔兽世界》准入时，宋建新重点强调了文化差异。“游戏是带有思想的，是带有意识形态的。”他说，“完全西化的东西不能直接拿过来，必须修改。”<br />
<br />
　　比如对于暴力的界定，就是审核中争议较大的地方。宋建新认为中国传统文化中对暴力的容忍度比较高。“比如古典小说里，张飞的长矛一捅，就把人捅穿了，再比如武松血洗鸳鸯楼。写是这么写，但网络游戏怎么表现，这是比较复杂的。”<br />
<br />
　　《出版管理条例》、《未成年人保护法》对血腥、暴力都有限制，与这些法律相对应，新闻出版总署有更为具体的标准。“不能有肢解行为和画面，比如 人头不能掉下，手不能被肢解，腿不能给砍了。社会公众对暴力的容忍标准，是逐渐变严格的，比如以前的连续剧《三国演义》中，人头落地是常见的，但是现在就 不行了。”宋建新解释说。<br />
<br />
　　中央高层关注网游<br />
<br />
　　根据新三定方案，文化部是网络游戏的行业主管部门，负责“在使用环节对进口互联网文艺类产品内容进行审查”，对网络游戏的内容与日常运营进行监管。在文化部内，负责具体工作的是市场文化司。<br />
<br />
　　文化部相关人士向本刊记者介绍，文化部对网络游戏的管理主要从三方面进行。一是主体准入，所有在中国境内运营的网络游戏企业必须取得运营资质； 二是内容审查，在中国境内运营的国外网络游戏产品须经文化部网络游戏内容审查委员会审查通过，符合中国法律、法规和基本社会公德后，才能在国内运营；三是 日常监管，对网络游戏企业运营行为、市场推广、消费者权益保障等环节进行监督管理。<br />
<br />
　　近日取缔一批黑帮网络游戏，背后就有文化部市场文化司的力量。<br />
<br />
　　千橡集团在接受采访时说，黑帮主题游戏并非他们原创，只是模仿美国SNS网站facebook上比较火热的游戏插件《Mobs》。 然而这些游戏却引起了中央高层的关注。文化部稽查处处长陈通告诉《望东方周刊》，7月20日，文化部接到了中央高层领导批示，4天后文化部就下达通知进行 了取缔。<br />
<br />
　　查处原因，一是未经批准、非法运营；二是内容违反国家现行法律法规，比如《互联网文化管理暂行条例》第17条第四款禁止的宣扬暴力、教唆犯罪。<br />
<br />
　　黑帮游戏的危害体现在哪里？陈通认为这些游戏是赞美黑社会，“违法的东西做得越充分、恶的东西展示得越充分，排名就越高。还专门设立了杀人排行榜。这些都有悖我们的道德体系、法律体系。现实生活中，是你考试分数越高，或者你助人为乐越多，做好事越多，你排第一名。”<br />
<br />
　　文化部市场文化司网络文化处处长刘强认为，这些游戏并不仅仅是对虚拟世界产生影响，对于网民的思想、行为同样会有深层次的影响，可能造成模仿，“在游戏中当老大的环节里，只有兄弟，没有是非，没有道德底线，见谁杀谁”。<br />
<br />
　　“未成年人是非判断力还不强，长时间玩游戏，会对暴力行为‘脱敏’。”刘强解释说，“如果长期沉溺于黑帮游戏，会对社会道德所不允许的东西麻木，在处理人际关系时可能会出现偏差，这对未成年人的成长伤害是很大的。”<br />
<br />
　　类似这样的网络游戏，文化部并非第一次查处。刘强介绍，文化部已查处了6批违法游戏产品。文化部执法的依据是《国务院对确需保留的行政审批事项设定行政许可的决定》(国务院第412号令)和《互联网文化管理暂行规定》。<br />
<br />
　　这6批被查处的网络游戏名单中，有涉嫌宣扬淫秽、赌博、暴力等违法内容的《吸血莱恩II》、《惊悚空间》、《AV麻将》、《终极刺客2：沉默杀手》、《恐惧杀手》等。<br />
<br />
　　刘强说，在文化部的内容审查上，只要符合中国的法律法规和社会公共道德，就能够通过。2004年文化部就下发了《关于加强网络游戏内容审查通 知》，而根据2009年4月的《进一步加强网游内容审查工作公告》，文化部建立了一套规范，包括一个内容审查申报网站，企业通过电子化的申报后，文化部进 行在线的功能处理，并公示审查结果。<br />
<br />
　　分级制度如何建立正在研究<br />
<br />
　　黑帮主题游戏在美国facebook上并未遭遇如此大的反对和阻力，是因为facebook必须是成年人才能够注册，这也就不存在对未成年人造成暴力脱敏的问题了。<br />
<br />
　　《魔兽世界》在美国也同样有年龄分级。在西方国家，分级制度很常见，不仅文学作品、影视作品有，游戏中也有相关规定。比如Rockstar旗下的多款涉及暴力的游戏，在西方都是最高级别。<br />
<br />
　　但是在中国实施分级制则存在明显问题。例如，如果建立了分级制度，那么很多适合成年人玩的游戏，就不会再按照保护未成年人的标准进行修改了。<br />
<br />
　　宋建新说，分级制度的实施，要认真研究并评估其可行性。他介绍说，新闻出版总署已经在研究这个问题。“现阶段来看，这只能是一种思路，还不具备可行性，要找出一个好的实施方式，不能头脑发热，简单照搬西方，要考虑到中国的现实国情。”<br />
<br />
　　他认为，要对目前的文化市场执法能力以及社会公众对出版物分级的心理承受能力等有清醒认识。“分级的愿望是好的，但立法、执法、管理以及相应宣 传教育工作要跟上，或者先行，否则，会造成原本不适合青少年的内容，因为分了级，一下子都传播出去了，其结果反而比不分级还糟糕。”■<br />
<br />
文/《t望东方周刊》</div>

							
							
							
							
