下令用新华社稿 中央严控传媒<br />
<br />
外传内地媒体不断炒作富士康接连发生员工跳楼事件，令郭台铭要向北京当局求助，宣传部门前晚即马上发出指令，要求各地传媒淡化事件，并且以官方新华社等稿件，代替记者报道。此外，当局亦严控网络，删除相关的帖子。<br />
<br />
网络清洗删帖子<br />
<br />
宣传部门的最新指令称，大批记者在富士康厂房及医院门外守候，已经严重影响其正常运作，当局要求所有媒体都要召回正在采访的记者。此外，各地报章的编辑亦被要求使用新华社的稿件，深圳报章接令不得报道前日跳楼不遂、割脉受伤的第 13宗自杀事件。不过，昨日出版的《南方都市报》仍刊出由记者采访的内容，以及有关第 13宗自杀事件的报道。<br />
<br />
当局对于新闻网站的规限更严格，有网站编辑指出，他们收到通知，网站只可以使用新华社通稿、政府消息或富士康的声明，而且网站要撤销所有专题报道。此外，网站的论坛、网志都不能出现新的帖子，讲述之前 12跳的帖子亦要锁定、淡化。<br />
<br />
据透露，当局更要求所有网站在昨晚进行清理，不得留有任何死角，违规、落实不力的网站都会予以重罚。<br />
