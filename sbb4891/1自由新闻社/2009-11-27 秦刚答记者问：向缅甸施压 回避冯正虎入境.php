2009年11月26日，外交部发言人秦刚举行例行记者会。答问如下。<br />
<br />
　　秦刚：女士们、先生们，大家下午好。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 问：“美国在台协会”主席薄瑞光日前在台湾称，美国尊重中国主权和领土完整，但这并不包括台湾。这是他第一次这么明白地说出来，并且是在奥巴马访华后的5天内迅速到台湾，做了一番解释。我们知道《中美联合声明》中双方都表示会“尊重主权和领土完整”，你对此有何评价？<br />
<br />
　　答：台湾是中国领土不可分割的一部分。尊重中国主权和领土完整这一原则当然适用于台湾问题。<br />
<br />
　　问：有两个问题，第一，日前台湾渔船被缅甸军舰扣押，请问此事有何最新进展？船员的状况如何？第二，美国商务部24日作出仲裁，决定向中国油井管征收反补贴关税，此事是否会影响中美关系？中方有何评论？<br />
<br />
　　答：24日晚，中国驻缅甸使馆接到缅方有关通报，称近日有10艘外国渔船及128名外国船员因非法捕鱼被缅方抓扣，其中包括4名台湾船员和1名大陆渔民。中国驻缅使馆正在进一步核实有关详细情况，并向缅方提出探视要求。外交部和驻缅甸使馆已向缅方提出交涉，要求缅方依法妥善处理，维护被扣大陆渔民及台湾同胞的安全与正当权益。<br />
<br />
　　我们一贯高度重视保护海外台湾同胞的人身安全和正当权益，中国驻外使领馆对广大海外同胞，无论来自内地、香港、澳门还是台湾，均一视同仁，全力维护其正当权益。<br />
<br />
　　关于美国对中国产的油井管征收反补贴关税的问题，昨天中国商务部已对此作出回应，我们对美方此举表示强烈不满和坚决反对。<br />
<br />
　　问：即将举行的中欧领导人峰会期间是否会举行记者会，温家宝总理是否会答问？<br />
<br />
　　答：对于即将举行的第12次中欧领导人会晤，中欧双方正在进行密切的沟通和协商。中欧领导人在会晤之后共同会见记者的具体安排还在进行之中。<br />
<br />
　　问：维权人士冯正虎近期一直在东京机场滞留，因为尽管持有合法中国护照，他却被中国有关部门阻止入境。你能否给我们一些细节？以及他为何被拒绝入境？<br />
<br />
　　答：中国有关部门根据《中华人民共和国公民出入境管理法》有关规定处理有关问题。我建议你向相关主管部门询问。<br />
