																											<div id="postmessage_846402" class="t_msgfont"><a href="http://news.sina.com.cn/s/p/2010-09-27/163321180588.shtml" target="_blank">http://news.sina.com.cn/s/p/2010-09-27/163321180588.shtml</a><br />
