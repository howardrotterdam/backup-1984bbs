<a href="http://www.sina.com.cn" target="_blank">http://www.sina.com.cn</a>&nbsp;&nbsp;2010年09月27日16:33&nbsp;&nbsp;东南网<br />
<br />
<img src="http://i3.sinaimg.cn/dy/s/p/2010-09-27/U4984P1T1D21180588F21DT20100927163319.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
詹爱村和老伴黄根华<br />
<br />
　　东南网-海峡导报9月27日讯(本网记者 蒋全德/文 梁张磊/图) 对很多五六十岁的人来说，红色歌曲是他们的青春记忆之一，而对于家住瑞景社区的詹爱村老人来说，以《东方红》为代表的红色歌曲，不仅仅是他们的记忆，还是情歌，甚至是救命歌――― 6年前，她正是靠唱这些红歌，把被车撞成植物人、昏迷了7个月的老伴黄根华唤醒的。<br />
<br />
　　詹爱村和她老伴今年都是61岁，两人来自龙岩新罗区。说到他们的相爱，詹爱村脸上露出了一丝红晕：“我们是在宣传队时相识相知的，他会拉二胡、吹笛子，而我歌唱得很好，两个人就这样好上了。21岁那年，我们结婚了。”尽管家庭条件一般，但黄根华做木工，努力赚钱养家，贤惠能干的詹爱村则操持家务，两个人很少红脸，“结婚40年了，我们吵架次数没超过5次。”<br />
<br />
　　然而，天有不测风云。2004年2月22日，黄根华骑着摩托车出去做木工时，被一辆皮卡车撞飞。“他的脑部受到重创，送到龙岩市第一医院后，一直昏迷不醒。住院10多天，没有任何效果，医院下了几次病危通知书，一些医生也悄悄告诉我们，放弃吧……”<br />
<br />
　　面对厄运的挑战，平时文弱的詹爱村一下子坚强起来：“刚开始我也很害怕，一个人时常流泪，但过了几天，我告诉自己一定要坚持住，孩子们在看着我呢。”于是，她跟孩子们都说：“我在电视上看过植物人苏醒的奇迹，我相信(奇迹)也能同样发生在你们爸爸身上，但需要大家一起努力。”几个孩子说：“只要爸爸有一口气在，我们就决不放弃。”<br />
<br />
　　于是，詹爱村和孩子们分工，自己主要负责照顾老伴，而孩子们去 筹医药费(肇事方赔偿的20多万远远不够)。然而，电视中几分钟内发生的奇迹，轮到自己在现实中一天天去实现时，詹爱村才发现这是那么难――― 由于事发时是冬天，天气很冷，为了防止老伴长褥疮，詹爱村每天除了用管子解决他的进食问题外，还要给他泡脚；为了唤醒丈夫的记忆，詹爱村每天跟他说话，并在他耳边轻轻哼唱着两人谈恋爱时唱的《东方红》等红色歌曲……<br />
<br />
　　他们的真情终于打动了老天爷。在黄根华昏迷了210天后，一天，詹爱村正在唱歌时，突然看见老伴的手动了，这让她看到了希望。考虑到医院房间小，康复治疗不方便，她把老伴带回家治疗。就在回家的半个月后，一天晚上，正在看电视的詹爱村突然听到一个声音：“电视真好看啊！”她一看坐在轮椅上的老伴，嘴巴正一张一合的，“他会说话了”。惊喜的詹爱村马上打电话告诉在厦门工作的儿女们，全家人兴奋得一夜未眠。<br />
<br />
　　然而，为了这个奇迹的出现，全家人都付出了艰辛的努力，尤其是詹爱村，不仅体重从老伴出事前的100多斤降到了78斤，还因焦虑过度患上了高血压。为了减轻母亲的负担，2007年，儿女们把父母接到了厦门居住。<br />
<br />
　　在老伴和家人的悉心照顾下，黄根华的身体恢复得很快，目前已能借着拐杖慢慢走路了。虽然口齿还不是很清楚，但谈到老伴对自己的恩情，他说：“等我好了，我要继续去做木工，赚钱好好报答她。”而听着老伴的话，詹爱村开心地说：“你要早点好起来，以后我们要一起去公园里唱歌、跳舞。”<br />
<br />
　　串串谭：奇迹之门 见证奇迹的时刻到了。<br />
<br />
　　放心，这不是刘谦在玩魔术忽悠你，而是用真实的例子告诉你：奇迹并非不可能。<br />
<br />
　　远在天边的，是近期一位澳大利亚母亲紧紧拥抱着已被宣布死亡的儿子，结果他死而复生了。<br />
<br />
　　近在眼前的，是福建一位女人天天在植物人丈夫耳边唱红歌，唱了200多天后，他被唱醒了。<br />
<br />
　　她们，让我想起了韦唯的歌：这是心的呼唤，这是爱的奉献，这是人间的春风，这是生命的源泉。<br />
<br />
　　一次次拥抱，一首首红歌，简单的动作背后则是坚强的意志与深沉的爱，在无私地奉献爱中，死神也望而却步。<br />
<br />
　　所以：奇迹之门只为心中有爱的人开放。 □红猪侠</div>

							
							
							
							
