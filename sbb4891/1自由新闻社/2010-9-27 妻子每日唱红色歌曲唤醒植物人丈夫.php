&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;1月25日下午，阎子彪的脑压终于得到了阶段性控制，却又因刀口感染开裂，呼吸微弱得几乎感觉不到――他距离生理性死亡仅有一线之隔了！好心的朋友们再次劝几位姐妹，既然子彪必死无疑，又何必为此把自己的一切全搭进去。二姐阎青兰第一个表态：“我同意再次手术，就是把我的厂子和全部家产全赔进去，我也绝不后悔！”在病房外的走廊里急得踱来踱去的二姐夫，猛地把已是身心交瘁的妻子揽在自己怀里说：“我也是这个意思。钱没了可以再挣，而兄弟只有一个。只是，我不愿看到你痛苦成这样啊！”<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;二次手术过后，阎子彪原本非常微弱的呼吸居然没有停止――他还活着！ 阎子彪因中枢神经严重受伤，致使舌头生理性后坠，堵住了气管。为防止病人因呼吸阻塞危及生命，医生只得按医学常规把他的气管切开，插入气管以维持病人的正常呼吸。但对身体异常虚弱的病人来说，无疑又多了感染的危险。唯一可以替代手术的方法是，必须一直有人一左一右地扶起病人的头，并随时关注病人舌头的情况。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 二姐阎青兰和三姐阎青芳，首先为弟弟做起了这种特殊护理。因为姿势单一，不能随便动弹，那种累可以想象。突然间，二姐青兰发现青芳嘴唇发紫、满头冒汗。原来，毫无知觉的弟弟不知为何咬住了自己的舌头，青芳情急之中就用自己的手指往开撬。弟弟的舌头避免了被咬断，青芳的手指却被弟弟死死咬住了。众姐妹好不容易撬开子彪的牙齿，塞进一双一次性软木筷子，青芳这才抽出自己已是血淋淋的手指。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;五姐妹血浓于水的殷殷亲情感染了医院的每一个人。脑科的刘主任除了尽最大努力挽救阎子彪的生命，也把阎家五姐妹视为自己的亲人，随时为其提供各种方便。脑科专家权大夫，每天都会主动前来诊查阎子彪的病情。他对阎家姐妹们说：“你们让我看到了人世间最感人的一幕，我打内心里感动，也更为子彪有这样的好姐妹感到幸福与自豪啊！”<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;植物人兄弟手摸女人乳房，大脑中竟然出现反应 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;手术后护理，可谓精细又艰苦卓绝。阎子彪的性命暂时保住了，却成了一个地地道道的植物人，大小便一点知觉都没有，全靠“自然”排泄。姐妹们一天几次地为其换被褥和擦洗身子，使他全身始终散发着香皂的清香。五妹阎青芬服侍哥哥时间最长，也最善于掌握哥哥各方面的规律，单说排尿，刚开始，哥哥总是尿床，后来五妹意外发现，预计哥哥快到撒尿的时间，只要她用手轻轻摁摁他的小肚子，他就会很“听话”地撒出尿来。自此以后，一到哥哥撒尿的时间，五妹总是毫不犹豫又非常熟练地一手扶着哥哥的下身，一手轻轻地摁他的小肚子。哥哥撒完尿后，她便仔细地为其洗下身和涂抹消炎药膏。五姐妹在山西博爱医院整整服侍了阎子彪3个月。阎子彪虽然长期卧床，却因护理非常精当细腻而没生过一次褥疮。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 阎子彪奇迹般地活下来之后，姐妹们又“贪心”不足了。她们听说长期进高压氧仓有助于植物人恢复记忆，便又一起筹款送阎子彪到省职业病医院做了3个月的氧疗。 阎子彪出院回家后，胳膊一直蜷曲着向上举着，肌肉硬得像石头，怎么也帮他伸不直。五姐妹又出高价请了一位按摩专家，每天定时为阎子彪按摩。3个月时间，她们花去了2万多元的按摩费，终使得阎子彪的胳膊放了下来。至此，从2000年1月下旬到同年7月，五姐妹共为阎子彪花去了近100万元，其中绝大部分为二姐阎青兰所出，为此她原本发展势头强劲的厂子几乎倒闭。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;然而就在这时，阎子彪的妻子却因在丈夫身上看不到任何希望，抛下孩子离家出走。五姐妹在惊讶气愤之后，很快也就坦然了：想走的就走吧，只要有我们在，六五就不会受罪。&nbsp;&nbsp;<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;2000年9月，阎子彪的脊背可以挺起来了。姐妹们给他买来轮椅，每天轮流推着他晒太阳、呼吸新鲜空气。尽管阎子彪对这些无知无觉，眼睛不眨，口水不断，依然是只比死人多一口气的植物人，但他身后的推车人却是满脸的虔诚与期待。她们的信心总是那么的执著与坚定――她们说给别人也说给自己听：都说“只要心诚，石头也能开出花来”，我们就不相信六五没有恢复记忆的那一天！<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;转眼间到了2002年春天。一天早上，三姐阎青芳正在为阎子彪穿衣服，忽然发现他两只胳膊抬了起来。“天哪，六五自己会动了――是他自己动的！”阎青芳顿时兴奋得大声喊叫起来。医生告诉她，这是病人的意识有所恢复的可喜迹象。 这种情况大约持续了一年，阎子彪的胳膊抬起的幅度越来越大，有时两手甚至可以高过头顶。但姐妹们很快发现，阎子彪的这种进步很快就具有了“破坏”性：他的双手经常乱抓乱挠，有时揪住自己头发，有时揪住自己的嘴巴或耳朵，没命地撕扯。有一次，他抓到了大姐的头发，硬是活活地揪下了一绺。医生的解释是：这是病人内心感到异常烦躁的生理反应，而他既然“会”烦躁，比之他此前的没知觉，无疑又是一个可喜的进步，也就是说，病人的意识还会有所恢复。 渐渐地，阎子彪每天“烦躁”的次数由1次增至3次，为防止他抓伤自己，五姐妹轮流“值班”，每天24小时守护。只是，他的眼睛依旧不会眨动，身体的其他地方依旧是一点都不会动。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;2002年夏季的一天中午，坐在轮椅上的阎子彪又是乱抓乱挠，烦躁不已。大姐阎青梅紧紧攥住他的双手，以防他抓伤自己。谁知，稍一放松，弟弟的双手猛地伸进她的领口，握住了她的两个乳房。阎青梅本能地躲避，却因他抓得太紧而没有挣脱。就在她不知如何是好的时候，发现弟弟突然安静了下来，只有按在自己乳房上的两只手微微动着。 阎青梅真是又羞又喜。这天下午，当弟弟再次烦躁不安时，她又牵起弟弟的双手放在自己的胸脯上。但这一次，弟弟的安静程度明显不如第一次好。她似乎有所明白，红着脸撩起上衣，把弟弟的手放在自己裸露的乳房上，弟弟果然再次安静下来。她的脸红得发烫，涌出的泪水也热得像在燃烧，她在心里对自己说：“就让我把他当做自己吃奶的儿子来照顾吧！”<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;阎青梅把这一发现悄悄告诉给一位医生朋友，要她解释其中道理。朋友在阎青梅的讲述中得知，阎子彪幼时由于体弱多病，跟母亲一个被窝睡到他12岁，每晚手握母亲乳房才有安全感，也才能睡得安稳，于是朋友分析说：人都是吃母亲的乳汁长大的，恋母情结与生俱来，而母亲的乳房，对你弟弟而言，在他的潜意识里，已成为他生命中的无形依赖，并成为他心目中“温暖”与“安全”的象征，他之所以握着你的乳房就安静下来，分明是把你当成了自己的母亲啊！身为优秀教师的阎青梅，马上有所悟地插话说：“如此说来，这女人的乳房还真成了唤醒我弟弟的星星之火了？”医生点点头，若有所思：“这女人的乳房或许还真成了唤醒你弟弟记忆的一个契机呢！”<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;“既然有了‘星星之火’，就一定会有烈火燎原的那一天！”阎青梅当天就把医生的话告诉了阎青兰和阎青芳。两姐妹在短暂的惊愕之后，说：“只要能治好弟弟的病，我们就豁出去了。” 她们三个是阎子彪的姐姐，裸露自己乳房的事，咬咬牙也就挺过去了。想不到阎子彪的两个妹妹，在这件事上也没有太多的犹豫，而她俩都是只有20多岁的青年女子啊！轮到五妹阎青芬服侍哥哥那天，家里来了许多男宾女客，而哥哥恰巧到了犯“烦躁”的时候。阎青芬深深吸了一口气，红着脸、当着那么多人，就把哥哥的一双手放在了自己全裸的乳房上。客人们惊呆了，但紧接着，他们又都感动得流下了眼泪，他们无比感慨地说：都说老嫂比母、老姐比母，想不到当哥哥遭遇不幸时，他的妹妹也像母亲一样啊！<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;等哥哥终于安静下来，在众人的议论中响起悠长鼾声的时候，阎青芬终于眼睛红红地哭出声来：“我是把哥哥当成自己的儿子来对待的，为了哥哥恢复记忆，我顾不了那么多啊！”所说的话，和姐姐如出一辙。几位女客人抱着阎青芬，再次泪流满面。<br />
<br />
&nbsp; &nbsp; 亲情无敌，五姐妹倾心救弟情撼一方&nbsp; &nbsp; <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;2003年2月初的一个下午，阎子彪手握着三姐阎青芳的乳房，眼睛眨了几下，猛然变得明亮起来。阎青芳对着屋内的爸爸妈妈惊喜地大叫：“你们快来看啊，六五的眼珠子会动了，有神儿了，他正在看我哪！” 这种情况持续了两个多月，阎子彪眼睛眨动的频率明显加快。有一天，五姐妹正好都在父母家，阎子彪握着一个姐姐的乳房，看看这个，再看看那个，眼眶中突然涌出了两道亮晶晶的液体。五妹阎青芬第一个惊叫起来：“妈呀，你们快来看呀，我哥会哭了，会哭了啊！”五姐妹一起望着阎子彪“咯咯”地笑，之后又都一起抱着阎子彪哭成一团。 阎子彪既然能哭了，他就一定能重新说话。五姐妹一起制订了教阎子彪认字、说话的具体方案。她们制作了许多小卡片，一个字往往几十遍、几百遍地对着他读。几个月后，阎子彪终于可以认得许多人和许多字了，拿出写有“爸爸”的卡片，他就指指爸爸；而指指正在忙碌的奶奶，他就能在一堆卡片中挑出写有“奶奶”二字的那张。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;有一天，五姐妹拿出写有“姐姐”“妹妹”字样的卡片，要阎子彪指认。这时候，就见用手握着三姐乳房的阎子彪，连续用嘴做了几个吮吸乳汁的动作，嘴唇吃力地嚅动着，嚅动着，终于迸出了一个字：“妈！”姐姐们连忙指指自己，又指指卡片帮他纠正说：“我们是‘姐姐’。”两妹妹也依照姐姐的样子帮他纠正说：“我们是‘妹妹’！”阎子彪点点头，又摇摇头，继续重复那个字：“妈！”说着，眼中的泪水一个劲地往外涌。 五姐妹明白了，六五已把她们视为母亲一样的至亲啊！五姐妹抱着阎子彪无语凝咽。爸妈哭了，83岁的老奶奶哭了，闻讯赶来的一大帮邻居，也都忍不住洒下了喜悦与感动的泪水。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;这受伤之后终于说出的第一个字，对阎子彪思维能力的恢复有着决定性的意义。在随后将近半年的时间里，阎子彪不仅学会说一些日常生活用语，还学会了玩扑克和打麻将。到2004年初，连姐妹们打牌时为了试验他的反应故意“耍赖”，每次都被他毫不客气地揪出来。又过了几个月，他甚至不用看麻将，用手就能摸出是什么。每天，他可以自己摇着轮椅在院子里转来转去，说是要早日锻炼好身体，以报答姐妹们比山还高、比海还深的恩德与情义。 尤其令姐妹们高兴的是，阎子彪还学会了为家里操心和关心别人。孩子们晚上看完电视忘记关掉，他就自己摇着轮椅帮助关掉，还对孩子说了一些节约用电的话。有时候他夜里睡不着，还会悄悄地来到爸爸妈妈和孩子们的房间，帮他们掖被角，扶枕头，或问他们需不需要喝水。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;2004年6月中旬，笔者在采访阎家五姐妹和阎子彪的时候，又意外看到了非常感人的一幕：身为警官的三姐，四肢着地，在又凉又硬的地上爬来爬去，一边爬，一边不厌其烦地向弟弟阎子彪讲解爬行要领。这是因为，阎子彪中枢神经已大部分坏死，他必须通过后天的锻炼，像婴儿那样地学习爬行和走路，其进程却要比婴儿慢得多。为了让阎子彪一点一滴掌握要领，五姐妹就一次次地在地上爬给他看。邻居家的几位三四岁小朋友感到滑稽好玩，笑得前仰后合，笔者却不由得一阵眼热鼻酸：即便是亲生母亲，也不过如此啊！ 五姐妹真诚地对笔者说：精心服侍阎子彪，让他一步一步恢复是她们五姐妹的共同追求。她们目前最大的愿望，就是通过各种方式方法让他先学会爬行，而后再学会走路。因为只有当他生活可以自理的时候，他才可以真正地享受生活，姐妹们和他也才有真正的幸福和快乐！<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;亲情无羞，亲情无忌，老姐小妹皆如慈母！阎子彪有这样的姐姐妹妹，是他今生之大幸！我们有理由相信，医学上的奇迹，很有可能再次在他身上发生。我们由衷地祝他好运！</div>

							
							
							
							
