<br />
　　据悉，纪念活动将以向毛泽东铜像敬献花篮、万人同唱《东方红》、万人长跑、万人同吃福寿面等“万人”规模活动为主，让海内外游客深刻感受到韶山的“红色”魅力和勃勃生机。<br />
