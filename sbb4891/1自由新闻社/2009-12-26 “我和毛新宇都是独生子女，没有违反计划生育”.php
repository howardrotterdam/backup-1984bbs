　　《法制晚报》报道，早上7时30分，记者在毛主席纪念堂前看到，前来瞻仰毛主席的人已经排起长队。天安门广场上升旗仪式结束后，越来越多的人加入到瞻仰毛主席的队列中。尽管气温低、天气寒冷，但这丝毫没有影响人们对毛主席的缅怀之情。<br />
