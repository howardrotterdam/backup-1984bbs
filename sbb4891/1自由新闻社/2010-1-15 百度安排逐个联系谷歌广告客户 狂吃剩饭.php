																											<div id="postmessage_432763" class="t_msgfont"><a href="http://www.sina.com.cn" target="_blank">http://www.sina.com.cn</a> 2010年01月15日 02:18 第一财经日报 <br />
　　孙进 <br />
<br />
　　“我感觉大家上班都洋溢着微笑。”中文搜索引擎巨头百度(BIDU.NASDAQ)相关人士昨日表示。因为受谷歌宣称将撤离中国市场影响，在周三的纳斯达克股市交易中，百度报收于439.48美元，大涨13.71%。而谷歌昨日最低到573.90美元，最高588.38美元，收盘报于587.09美元，全天仍跌了0.57%。 <br />
<br />
　　因为百度曾实行全员持股策略，目前很多员工手中还持有股权。谷歌如果放弃30%(目前谷歌在中国搜索市场的占有率)多的中国市场份额的话，百度目前60%以上的市场份额有望上涨到90%以上。 <br />
<br />
　　“幸福来得太快。”百度员工表示。而百度负责广告销售的员工在早上来到公司后，第一件事就是列出谷歌的广告客户名单，开始逐个联系。“我们内部调侃这是捡钱计划。” <br />
<br />
　　谷歌的内部员工表示，广告系统还在正常运作，客户的广告都在正常显示。公司没有像外部说所的运作出现问题。只是一些员工代表参与了当天的总部视频会议连线，大家都在等待解决结果。如果一旦解散，广告部的员工大部分会领几个月的补偿，而研发工程师们可能会有人留下。 <br />
<br />
　　原谷歌中国首席营销官(CMO)王怀南透露，谷歌中国员工大部分是负责广告销售，一部分研发人员负责支持中国和全球的研发任务。 <br />
<br />
　　假如谷歌未来真的退出中国市场，花旗分析师凯瑟琳・梁在研究报告中称，百度可能会抢到谷歌留下的中国市场份额的大部分。 <br />
<br />
　　目前，谷歌在中国有大量的商业合作关系，包括天涯、迅雷、中国移动(00941.HK)和新浪(SINA.NASDAQ)。如果谷歌一旦撤出，将留下庞大的流量空白。比如新浪就与谷歌有搜索广告分成合作协议，一旦谷歌撤出，留给新浪选择的合作伙伴并不多。 <br />
<br />
　　新华都(002264.SZ)CEO唐骏表示，其实搜索引擎的用户和广告客户黏性都不高，一旦市场地位出现问题，很容易转化和流失。谷歌在中国不仅有搜索引擎和广告网络，谷歌的操作系统、办公软件和手机平台都在中国市场有布局。中国市场对其全球布局不可或缺。 <br />
<br />
　　有百度员工表示，从某种意义上说百度CEO李彦宏的预言正在成真。事实上，在2006年的一次论坛上，零点研究集团董事长袁岳问李彦宏：“5年以后Google和百度是什么样的关系？”李彦宏说：“5年以后，Google和百度之间的关系是百度一枝独秀，从市场份额来说，大家很难看到Google了。” <br />
<br />
　　而百度和谷歌的竞争关系将发展到中国之外。百度的内部目标是在2012年与谷歌展开“划洋而治”的竞争，在太平洋西面的日本和东南亚等市场，实现百度全球化的目标。</div>

							
							
							
							
