<img src="http://img.hexun.com/2009-09-10/121028630.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
30辆清一色的黑色奔驰轿车整齐列队迎接藏獒。<br />
<br />
　　西安咸阳国际机场现豪华迎“宾”队伍<br />
<br />
　　30辆大奔接狗 观者咂舌<br />
<br />
　　30辆清一色的黑色奔驰轿车排列整齐地停在西安咸阳国际机场的停车场，这个豪华车队是为了迎接一只身价400万元的藏獒。如此气派的迎接队伍引来不同的声音。有人说，这样太奢侈，有“炫富”嫌疑。<br />
<br />
　　30辆奔驰列队机场等“贵宾”<br />
<br />
　　昨日下午2时30分许，在两辆“三菱”越野车的带领下，30辆清一色的黑色奔驰轿车列队驶入西安咸阳国际机场停车场，引起不少过往乘客驻足观看。<br />
<br />
　　“不知道哪位贵宾要来西安？”不少人都小声议论着。30辆黑色奔驰轿车中，西安本地牌照的不少，还有北京、天津牌照的车辆，全部为S500、S6OO系列。<br />
<br />
　　“贵宾”是条狗 身价400万<br />
<br />
　　10分钟后，所有车辆停妥，一幅写有“‘长江二号’以400万元引至西安，落户××獒园”的条幅挂上了越野车，这时大家才明白，这么多奔驰原来是为了迎接一只藏獒。<br />
<br />
　　在众人的期待中，下午3时30分左右，“长江二号”终于出现在了机场货站，大家“呼啦”一下子围了上来，争着一睹它的芳容，“太漂亮了！纯黑色的，还真不多见。”铁笼打开，“长江二号”威风凛凛走了出来，这是一只身高七八十厘米的黑色雄性藏獒。迎接的人员马上为它系上了大红花。<br />
<br />
　　一名机场安检人员甚至暂时放下手中的工作，看看到底是一只什么样的藏獒。听说眼前这只藏獒值400万元人民币，这位女安检工作人员吃惊不小：“在这里上班五六年了，见过不少托运的名贵动物，从没见过身价这么高的藏獒，更没见过这么气派的迎接队伍。”<br />
<br />
　　藏獒爱好者自发组队迎接<br />
<br />
　　牵着“长江二号”走下货场的是一男一女，年轻女子姓王，“长江二号”现在的主人，从青海玉树到西安，一路上她一直陪着“长江二号”。“来合个影！”“长江二号”犹如一位“明星”，显然已经习惯了各种各样的场合，听到指挥后温顺地看着镜头。<br />
<br />
　　王女士说，“长江二号”是后来才取的名，它原本有个好听的名字，叫“白根”，今年1岁半，是她和朋友花了很长时间才物色到的一只原生态藏獒，是名獒“苏牧王”的后代。“金子有价獒无价”，王女士说，这次去玉树，原本是为自己的一只藏獒配种的，由于太喜欢“白根”了，才临时决定花400万元买下了 “白根”。说起如此庞大的迎接队伍，王女士说，这些迎接的朋友不少都是藏獒爱好者，大家都是自发组织起来特意迎接的。<br />
<br />
　　豪华迎接队伍令路人咂舌<br />
<br />
　　1个小时后，浩浩荡荡的迎接队伍驶离机场，朝着西安市区挺进。<br />
<br />
　　为迎接一只藏獒，居然动用这么多车，且都是名车，不少目击群众表达了不同的看法。“难道狗比人金贵吗？”“是不是有点太讲究排场了？”一位等亲友的先生连连咂舌，“以前见过兴师动众搞迎接的，但阵势这么大，还真没见过。来这么多的车，这样的排场不能提倡！”<br />
