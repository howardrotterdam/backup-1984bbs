<br />
为维护劳动人民的切身利益，学法用法颇有经验的同志介绍了如何依法维护自身权利，特别是房屋拆迁问题，要注意区分国家公共用地与商业开发用地的法律区别，商业用地必须平等协商，任何强制拆迁，都是违法的。</strong><br />
