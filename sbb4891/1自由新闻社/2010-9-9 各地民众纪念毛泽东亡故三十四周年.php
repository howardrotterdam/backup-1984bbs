													<h2>河南省通许县人民群众隆重纪念伟大领袖毛泽东主席逝世34周年　　</h2>
						
						
																											<div id="postmessage_821083" class="t_msgfont">作者：遍地红花&nbsp; &nbsp; 文章发于：乌有之乡&nbsp;&nbsp;更新时间：2010-9-9&nbsp;&nbsp;<br />
<br />
2010年9月8日　下午，在河南省通许县城的中心广场上，人群熙攘，广场东侧更是热闹非凡，人们三三两两的交谈着议论着，有的则是咒骂着、呼喊着，甚至是激动的发泄着积压在心底多年的愤懑。<br />
<br />
 通许县老工人老干部老党员学习毛泽东思想读书会要在这里举行“纪念伟大领袖毛泽东主席逝世34周年”红色文艺演唱会。三级台阶之上的平台，耸立着近30平方大小的巨型幕布，幕布上巨幅毛主席挥手向人民致意的画像特别引人注目。<br />
<br />
台阶下两侧走廊拱道的石柱上对称的悬挂着两幅喷绘的巨型横幅，每条横幅上各有四句标语，左边的一幅上写着“没有毛主席就没有新中国”、“只有社会主义才能救中国，只有社会主义才能发展中国”、“毫不动摇的发展公有制经济――胡锦涛”、“搞私有化走资本主义道路只能死路一条”，右边的一幅上写着“任何时候任何情况下我们都要高举毛泽东思想伟大旗帜――胡锦涛”、“发展为了人民，发展依靠人民，发展成果由人民共享――胡锦涛”、“如果我们的政策导致了两极分化，我们的改革就失败了――邓小平”、“如果出现了什么新的资产阶级，我们就真的走了邪路了――邓小平”。<br />
<br />
 就是这两个横幅上的八条标语引起了人们的热议。有一个老工人说：“咱这江山就是毛主席打出来的，有些人光想跟毛主席比，第几代，第几代，他差远了，给毛主席提鞋也不中......”有人接着说：“别说提鞋，提尿罐也不中。”又有人接着说：“他不是说永不翻案么？毛主席说永不翻案靠不住，毛主席算是把他看透了......”还有一堆人在那议论着公有制私有制的事，有个白头发的老人说：“胡锦涛叫咱毫不动摇的发展公有制经济哩，可咱们这儿还有多少家公有制了？”另一个稍微年轻点的人说：“妈那个X，啥公有制啊，还有一家没？弄来弄去，公家的东西，咋一转圈，都弄到他们鳖孙自己的腰包里了，我日他万奶奶，都好过他们妻孙个人了......”<br />
<br />
右侧的一堆人，正议论着两极分化的事，一个戴眼镜的中年人说：“我看现在不叫共享发展成果，要共享，人人都富得流油了，哪还有恁多穷人？”有个留着胡子的人说：“现在还不算两极分化么？一个富人朝咱几十个。”另一个老人插嘴说：“啥？几十个？光得几百了！”一个穿着破布衫的中年人说：“几百也不止，听说啥保险公司的老总一个姓马的老总，一年六千多万年薪，朝咱多少？你一年一万块钱中不中，朝咱六千多倍。”有人说：“几万个农民的收入，还说没有两极分化，骗谁啊？！”有人说：别说恁些大厂矿的老总们了，他们都是一年好几百万，那些资本家哩，几亿几十亿的财产，朝咱多少？几千倍几万倍也不止，朝咱几十万人的收入，这还不是两极分化么？非得叫全国的钱都叫他们弄了才是两极分化？”有个低个的中年人说：“现在有些人呀，是说人话不做人事儿，啥叫打着红旗反红旗？<strong>现在有些人，连邓小平的话都不听了，口口声声不能走回头路，啥叫回头路？毛主席领着是向共产主义走哩，他们扭过头去往资本主义路上走开了，这都走到解放前了，解放前不都是一家一户，单打独斗各顾各，现在不也是这？还不叫回头，咋着，一直走到老蒋那时候呀，走到满清那时候呀！</strong>不回过头来走毛主席指的路，就只能像那标语上写的样‘搞私有化，走资本主义道路，死路一条’......”<br />
<br />
 ......广场上充满了对毛主席的赞扬和歌颂，也充满了对资改派、汉奸卖国贼的声讨和咒骂。<br />
<br />
夜幕降临，华灯初放，广场上人越来越多，七点半左右，平台下的广场已黑压压的坐满了人。两个两千瓦的高亮射灯，照的平台一片光明，读书会红色艺术团的团员们，闪亮登场。红歌合唱团清一色的红上衣白裤子，几十个人排了四排，在台阶上显得格外光彩夺目，十多人的乐队敲响了开场的锣鼓......<br />
<br />
 主持人宣布，纪念会开始，首先由学习毛泽东思想读书会老工人老干部老党员代表李长学同志致怀念词。<br />
<br />
李长学同志是一位七十多岁的老同志，但他老当益壮，身体健康，坚定的红色信仰使他显得像四五十岁的壮年人，他声音宏亮，慷慨激昂，他开始用近乎悲哭的声调首先回顾了三十四年前主席逝世时的场景，他说，主席逝世的噩耗像晴天霹雳震动了中国、震撼了世界......人们停止了一切劳动，沉浸在极大的悲痛之中，人们哭喊着“我们的天塌了，天塌了！”悲痛的哭声覆盖着大地，滚滚的热泪汇成了江河，这发自肺腑的痛苦的呼声感天动地，大地也为之抽泣呜咽，老天也为之落泪动容......<br />
<br />
李长学同志还回顾了当时全世界各国各地几乎都举行了声势浩大的悼念仪式，成千上万人川流不息的到我驻各国大使馆吊唁......还回顾了当时世界各国通讯社和新闻媒体大量的报导活动。说：在宣布主席逝世后几个小时内，世界各地对他的赞扬如潮水般涌来......李长学同志还回顾了全世界各阶层的人们都对毛主席做出了极崇高的评价，他说，毛主席不单是伟大的思想家、革命家、理论家、战略家、政治家、军事家，历史家、文学家，还是杰出的诗人、书法家，是通古博今、峥嵘百家、才华横溢、智慧超群、经历丰富、实践宏伟、大谋大勇、出类拔萃、古今罕见的奇才，是品德高尚、人格伟大、生活俭朴、毕生鞠躬尽瘁、全心全意为中国人民和世界人民服务的伟大领袖和导师。<br />
<br />
李长学同志说：“毛主席逝世后三十多年来，中国和世界历史的发展已充分验证了所有对毛主席的崇高评价是完全正确的，切合实际、具有远见。<br />
<br />
最后，李长学同志愤怒的说：“可悲的是，有那么一小撮卖国反人民的民族败类，竟冒天下之大不韪，与人民为敌，不惜采取各种卑鄙手段，极力贬低诋毁毛主席，反毛诬毛极力向毛主席身上泼污水，他们的罪恶目的就是要彻底否定马列主义毛泽东思想，彻底否定无产阶级革命和无产阶级专政，彻底否定社会主义和共产主义，把中国完全推向西方资本主义，我们广大人民群众是绝不会答应的，我们要与他们进行坚决的斗争。”他还说：“马列主义和毛泽东思想是科学，是颠扑不破的真理，在某种情况下，真理也可能一时受挫，但最终，真理必定战胜谬误，正义一定要战胜邪恶，斗转星移，时间飞逝，毛泽东思想将会永恒，时间越久，越能显示他的伟大和正确，时间越久，人类就越离不开他，这是历史的客观规律，不是谁想改变就能改变的，任何人想改变他，想贬低他，想诬蔑他，只能是螳臂当车，只能是自取其辱，只能留下历史的骂名，只会留下历史的污点，只会给后世留下笑料......”<br />
<br />
李长学同志最后说：让我们高高举起毛泽东思想伟大红旗，坚决捍卫毛主席无产阶级革命路线，要斗私批修，为捍卫中国共产党，捍卫毛主席所开创的社会主义事业，捍卫无产阶级和广大劳动人民的利益而努力奋斗。<br />
<br />
李长学同志的怀念讲话，博得了台下广大群众的阵阵热烈的掌声。<br />
<br />
接着，由读书会红歌合唱团为广大群众演唱了《东方红》、《毛委员和我们在一起》、《毛主席的话儿记心上》、《咱们的领袖毛泽东》、《毛主席的书我最爱读》等十四首红歌。<br />
<br />
接着，由读书会红色艺术团戏剧演唱队为广大群众演唱了《红灯记》《杜鹃山》《红色娘子军》等革命样板戏十二段选段。<br />
<br />
 最后，台上台下共同高歌《大海航行靠舵手》，雄亮的歌声响彻广场，震撼夜空，将纪念会推向高潮。<br />
<br />
纪念会结束，很多群众久久不愿离去，他们说：“好久没有听过这样的演唱了，太感动人了，让我们明白了很多东西，受到了很大的教育和启发。”</div>

							
							
							
							
