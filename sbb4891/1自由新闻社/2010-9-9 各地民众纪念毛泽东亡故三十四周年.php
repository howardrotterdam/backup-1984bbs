2010年9 月4日，来自昭通、大理、玉溪、楚雄、寻甸、以及昆明地区工矿、企事业单位的干部、群众50多人，聚集在昆明，隆重纪念伟大领袖毛主席逝世34周年。原昆明市妇联副主任王云仙同志，身体不好，行动不便，她坐着轮椅也要来参加纪念会，大家为它的精神所感动。<br />
<br />
会场布置得壮严肃穆，“深切缅怀毛主席”的大幅标语下,挂着毛主席像,对面墙上，挂贴着马克思、恩格斯、列宁、斯大林毛主席像。<br />
<br />
会议由原国防工办副主任，党组副书记李树林同志主持，会议开始，播放了1976年9月9日毛主席逝世后，全国各族人民失去伟大领袖毛主席悲痛情景，和北京人民大会堂，中央领导、各界代表与毛主席遗体告别的录像（节选），看到此情此景，个个老泪纵横，大家沉浸在悲痛之中。<br />
<br />
97岁老红军郑加平的书面发言，题目是《恶意谩骂伟大领袖毛主席者，是汉奸卖国贼》，由马首良同志代为宣读的。郑老应用了毛主席逝世后，各国政要发来的唁电，讴歌毛主席对中国革命，世界革命所作出的伟大贡献，毛主席不但是中国人民的伟大领袖，也是世界革命人民的伟大导师。郑老对当今社会上刮起的一股非毛、反毛、妖魔化毛泽东的妖风，进行严厉的批判。他说：“毛主席是中国共产党的缔造者，是人民军队的缔造者，中华人民共和国的缔造者，是中华民族的英雄，是我们的长辈，是共产党的老祖宗，对毛主席的恶意谩骂、丑化，已经超出了一个民族应有的理智界线。一个民族不尊敬自己的民族英雄，是一个可悲的民族。一个国家连自己的长辈、祖宗都要谩骂、丑化，完全丧失了一个国家的尊严，丧失一个国家的民族气节”。最后，郑老气愤地质问：“<strong>我们国家的领导人，为何对辱骂领袖毛主席的反动行径不置一词？而任其所为？？？你们在世界人民面前还有民族尊严、还有民族气节吗？</strong><br />
<br />
纤夫同志发言的题目《反毛民必反》，他说：“纪念毛主席，就是纪念革命，毛主席是中国无产阶级革命之父，他是革命、造反的化身，是继续革命的灯塔，毛泽东思想是最终实现共产主义的保证；纪念毛主席，就是纪念社会主义，就是表达我们要重建科学社会主义的愿望和决心；纪念毛主席，就是纪念我们自己，我们跟着毛主席参加了无产阶级文化大革命，实践了反修、防修的演习。毛主席领导人民翻身解放，当家作主人，毛主席恩泽人民，人民深深热爱毛主席。那些疯狂攻击、污蔑、丑化和谩骂毛主席的走资派、汉奸卖国贼，必然遭到人民的反对。反毛民必反！！！<br />
<br />
纪念会上，包亚芳、杨仁民发言认为：毛主席关于无产阶级专政条件下继续革命的理论，丰富、发展了马克思列宁主义，是共产主义运动史上，马列主义发展的第三个里程碑。无产阶级文化大革命，是继续革命理论指导下的第一次伟大的革命实践，尽管遭到失败，但是无产阶级文化大革命的生命力、现实意义、深远的历史意义，在现实斗争中，充分发挥其威力、作用。我们有幸参加了毛主席一生所做的第二件大事，感到无上光荣，无比自豪，无怨无悔。<br />
<br />
正确评价无产阶级文化大革命，是我们的历史使命，因为在中国共产党的历史上，这是跨不过、绕不开的一个历史阶段。采取掩耳盗铃、不准谈文革，不准讲揭批查运动制造了大量的冤假错案，其实是害怕文革。文革是一面镜子，他们不敢面对，文革运动中，全国人民所反对的、批判的，现在他们正在积极、卖力地宣传、推广、实施着。对任何一个事物，肯定一切，否定一切都是错误的，正确评价无产阶级文化大革命是必然的，是回避不了的原昭通地委党办主任张占云同志说：“毛主席逝世后，华国锋成了党政军的一把手，我说过，华国锋资历浅，是个傀儡人物，从此我成了反革命，受到隔离审查、批斗，更为残忍的是，把我的16岁的女儿也抓起来审问，孩子说：“我爸爸是好人，你们不该抓他”。看守人员恶狠狠的说：“你说你爸爸是好人，说明你就是坏人！”根据这个“逻辑”，就把我的女儿也关进了监狱。<br />
<br />
这是一个坚强的孩子，她在牢房里大唱“含着眼泪绣红旗”，“跟我爹爹打豺狼……”看守人员就把她双手铐起，吊在门上，她不求饶，孩子在监狱里受尽折磨，经常吵闹又无法判刑，关了2年多，才把他放了，没有任何说法。这是什么世道？<br />
