<br />
楚雄市代表李承滇的发言，旁征博引，深刻论述世界、和我国的先辈、贤人们，也曾努力寻找过从动物世界丛林法则超脱出来，进入“大同世界”的途径，建立“天下为公”的社会，都不可能实现。<br />
