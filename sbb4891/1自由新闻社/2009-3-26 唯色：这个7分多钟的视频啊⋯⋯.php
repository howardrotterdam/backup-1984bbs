1、0:00-0:50 时间：2008年3月14日。地点：拉萨小昭寺门前。内容：藏人抗议，便衣出现。<br />
<br />
2、0:50-0:57 时间：2008年3月？日。地点：拉萨。内容：特警抓捕藏人。<br />
<br />
3、0:57-1:00 时间：2008年3月15日。地点：甘肃省甘南州夏河县。内容：藏人僧俗民众上街游行。<br />
<br />
4、1:00-1:25 时间：1988年3月？日。地点：拉萨大昭寺。内容：武警冲进大昭寺殴打、抓捕僧人。<br />
