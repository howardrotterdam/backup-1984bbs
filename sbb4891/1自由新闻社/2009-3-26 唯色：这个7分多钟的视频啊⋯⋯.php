<br />
可是这个视频，虽然引起媒体重视，但也被称“其中一些录像明显地是新旧录像混用，很难确定拍摄时间和地点。”<br />
<br />
是的。这个视频的制作有令人遗憾、惋惜之处。就我看来，其中的一个部分，即1988年在拉萨，军警冲进大昭寺殴打僧人的录像，原本可以不放在这个视频中。因为许多有关西藏的纪录片、视频，都采用过这段录像。<br />
<br />
