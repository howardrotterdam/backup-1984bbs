<br />
应该更精炼，更精炼……<br />
<br />
不要音乐……<br />
<br />
要字幕：时间；地点；而且是中英文字幕。中文字幕不可少！<br />
<br />
――当然，这只是我一己之见。<br />
<br />
反复地看这个7分多钟的视频，我做了一份简单的记录如下，希望对看过视频却不了解情势的人有所帮助。若有写错，还望指正：<br />
