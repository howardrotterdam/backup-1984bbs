													<h2>前《文汇报》驻大连记者姜维平回忆薄熙来</h2>
						
						
																											<div id="postmessage_217178" class="t_msgfont">注；姜维平是伯西来的克星<br />
<br />
打印版 【 阿波罗新闻网2009-08-27讯】 作者：姜维平&nbsp; &nbsp;<br />
 　 <br />
虽然时间的长河流逝了整整10年，但Jiang Zemin1999年8月的大连之行，还在记忆中清晰地留存，由于中共操控的媒体的虚假报道，使许多接近其人的消息来源，如同水下的礁石不得不深藏隐匿，但只需拂去水面的恐惧与胆怯的枯枝败叶，就会生动地展示它潜流般的事实真相。<br />
<br />
题词并非Jiang Zemin原创<br />
<br />
号称北方明珠的大连，在1999年步入了百年建市的节日，大连市委市政府拟搞隆重的庆祝活动，这并不为过，但野心勃勃的大连市委书记薄熙来另有谋略，他要借此造势，争取当上中共中央委员，辽宁省长，省委书记，而此前一方面他通过土地批租，国企转让，资本运营，己结交了京城的众多权贵，另一方面以闻世震为首的辽宁地方势力，不买他的账，与其明争暗斗，依然强占沈阳。在这种情况下，他必须紧紧地卖身投靠Jiang Zemin，才能力排众议，冲进省城任职，因此他绞尽脑汁，借大连百年庆典为借口，先亲自带领马崽去北京江宅求见，美其名曰“大连百年，大连551.5万人民渴望领导亲笔题字”，并且送上大份子“润笔费”，立即见钱眼开的Jiang Zemin欣然命笔，却枯思无词，一时无语。薄熙来早有准备，伴随着阿谀奉承的奸笑，取出大连市政府办公厅秘书皇甫某涛拟好的诗句多篇呈上，Jiang Zemin用手翻了半天，从其中选了两句：“百年风雨冼礼，北方明珠生辉。”薄熙来马上说，这句怎么样？我琢磨了好几天啊！……Jiang Zemin扫了一眼薄放在桌上的红包，立即眼晴一亮。薄是行贿高手，红包虽薄，但银联卡一张外加带有秘码的信函一封，江书记举手之劳何乐而不为？他取出毛笔，10分钟就交差了事，尔后薄熙来捧在手上，如获至宝，立即亲自找人装裱去了。<br />
<br />
后来，跟随薄熙来赴北京的皇甫某涛对我说，Jiang Zemin也没什么文化水平，题个字给大连，还得由我们先拟稿，薄市长研究半天，最后满意了才呈送上去，实际上我当时构思了5个句子，这个并非最精彩的一个，但薄市长高兴，因为说大连是“北方明珠生辉”，这等于Jiang Zemin充分肯定了他的工作成绩。<br />
<br />
那时，我把皇甫某涛写的一篇纪念诗人公木的文章，推荐给了香港文汇报副刊部主任方芳，她很快在报上以显著位置发表了，所以皇对我很交心。他经常到我的办公室混饭吃，席间告诉我说，他是满族后裔，原毕业于吉林大学中文系，是公木的得意门生，原在锦州市政府工作，因慕名崇拜薄熙来，写信给他以示效忠，被其破格录用。他说：题词这个事，薄熙来这说是“国家机密”，绝对不能告诉任何人，别人问起，就说是Jiang Zemin的创作……<br />
<br />
也就是这次举足轻重的北京之行，他协助薄办了一件大事。Jiang Zemin答应薄熙来，将在8月中下旬亲自到大连参观考察。皇甫某涛说，只要Jiang Zemin来了，咱们的薄市长就将离开大连，步步高升了。以后最起码也是国务院总理。<br />
<br />
为什么？这怎么可能！我一脸迷惑，但皇甫某涛故意卖关子，说：你等着瞧吧！<br />
<br />
<br />
薄熙来变成了“大导演”<br />
<br />
1999年8月10日至15日，Jiang Zemin首先带领国家主席Wu Bangguo等人，在沈阳，鞍山等地进行了巡视之后，然后赶到了大连，召开了“东北和华北地区八省市国有企业改革座谈会”，并由新华社记者傅新雨发表了题为《Jiang Zemin在辽宁考察强调：解放思想，实事求是，推进国企改革》的报道，尔后情不自禁地走上了薄熙来为其搭起的舞台，成了他精心策划的一场闹剧里丑陋的角色。<br />
<br />
此前，薄熙来不仅导演般预设了行走路线，选用了一些接待Jiang Zemin的铁哥们，还精心编好了脚本与台词，并实地进行了多次演练。<br />
<br />
首先，在沈大前盐高速路口至大连棒棰岛宾馆途中两侧道边，种上一路草坪，外加方砖步道，在中山路上挂上了Jiang Zemin的巨幅画像，旁边写上江的题写：百年风雨冼礼，北方名珠生辉。原本这些位于交通要道的路牌灯箱，早被大连鑫新广告公司老板李某安买去了，刊登了产品广告，卖给了东北某企业，但薄熙来一声令下，撕毁了合同不算，广告公司还要赞助制做Jiang Zemin彩色画像。李老板对我说，薄市长亲自审稿签字，才敢挂出来呀！我的经济损失大啦！<br />
<br />
当然Jiang Zemin做为一国之君，要安排在捧捶岛宾馆的5号楼下榻，当年这家由市政府交际处管理的国宾馆，给毛泽东专门建了一拣别墅叫5号，但毛泽东嫌辽宁粮食不能自给，总吃“返销粮”而颇为生气，故生前拒来此处享乐，Jiang Zemin没有这个牛气，他认为不管别人怎样，自已先住进小楼享享清福再说。这里有山珍海味，美酒佳肴，到处青山绿水，空气清新，夏日还有天然的海滨浴场，正是避暑圣地，Jiang Zemin带上太太与孙子，正是享受天伦之乐的好时光。薄熙来派国安局特工与中央警卫局密切合作，把国宾馆围个水泄不通。据接近大连国安局党委书记车克民，局长万国涛的人士透露，薄熙来下令，任何反对他的人，不论是老干部，还是机关在位的官员，都不能让他们接近Jiang Zemin的陪同人员，以免把他在大连的施政缺失泄露出去，他们还梳理了一份37个的黑名单，上面有认识Jiang Zemin小姨子王某{大连海事大学教员}的市统计局信息处孙某副处长等人，薄说，再认真查一下，与由喜贵，贾廷安等有联系的人，大连还有谁？统统给我看住，别让这些坏蛋坏了我们的好事。{薄的口头语，骂人是“坏蛋”“傻蛋”，“混蛋”}这回Jiang Zemin来大连是关健时刻，因为以后用不用我，上面争议挺大的，江主席要亲自看一下，我要是上去了，你们自有安排，我要是下来啦，闻世震，于学祥就会整死你们！这几年你们做了什么，不要以为人家是傻蛋，不知道！<br />
<br />
这些人在大连多年来仰仗薄的伞荫，以权谋私，敲诈勒索，行贿受贿，无恶不作，其捡举材料成麻袋，早在中央与省纪委存放，只是不便查办而已。于是这些死党为了保住既得利益，立即行动了起来。<br />
<br />
在薄的授意下，为了让Jiang Zemin在车行沿途看到花草树木，不仅精心地选择了最佳路线，还在通往捧棰岛的途中，种满绿树草坪，尤其是离宾馆3000米以内的道边，更是煞费苦心，部分裸露的地表，全部用高价买来的草苗遮挡，还在有可能缓行的坡道与茬道边，故意布置了装扮成普通市民样子的“特务”，整天恭候在那里。其实这些人早就背好了台词，一旦讧译民中途下车行“亲民之问”，一定会用标准的海蛎子味大连话，猛讲薄熙来当政的丰功伟绩。用薄市长编的广告词慨括，是“金杯，银杯，不如老百姓的口碑”。{后来百姓骂薄“这个杯，那个碑，都是薄市长逼咱背}Jiang Zemin咧开了蛤蟆嘴笑呵呵<br />
<br />
虽然，自从1976年毛译东死了之后，他的画像还挂在北京天安门上，邓小平等历任党的领导人还没有谁敢仿照毛泽东，搞画像敬拜这一套，但薄熙来为了以示忠心，在时隔23年之后，在大连市中山路197号附近，挂起了巨幅彩色Jiang Zemin画像，凡是往来市政府大楼的车辆与行人，无一不通过此处，而且这里又是前往星诲湾的必由之路，原定Jiang Zemin要在8月20日上午参观中国科学院化物所，而这家世界著名研究所的新址，即在风景秀丽的星诲湾。薄熙来命令下属买通了Jiang Zemin的司机，故意在车辆到达中山路松山街时，放慢了速度，Jiang Zemin认出了道南这栋洋气的小搂就是大名鼎鼎的化物所旧址，又猛一抬头看到了自已的半身画像，与不久前的题词，赫然耸立。他立即眉开眼笑，满脸油光，一张大蛤蟆嘴咧得大大的，不停地点头。后来薄市长听说了这个好消息，一夜高兴得没睡。陪同Jiang Zemin的辽宁省长张国光对人说，没想到小薄拍马屁，没有拍在蹄子上，这要是换了朱总理，还不得发火？据这次参与保卫工作的人士披露，薄立即把这话告诉了江，江很生气。这正是不久后改任湖北省长的张国光被抓捕坐牢的原因之一。<br />
<br />
Jiang Zemin盯住自已的巨幅画像，从正面看了半天还不过瘾，还在车辆驶过几秒钟之后，又依依不舍地回头望了一眼，确定这画像是货真价实的双面画像才放心。薄熙来特别细心，把画像两面设计的尺寸颜色图案一点不差，这使好大喜功的Jiang Zemin合不拢嘴。<br />
<br />
然而，薄市长也有担心之处，在亚洲最大的城市广场上，建有一个象征着封建帝王的华表，这可能令Jiang Zemin不快，因为当年在修建大连滨海路时，工人们不小心用火药炸坏了南大亭一块类似龙头的山崖，薄市长花重金请的风水大师说，这不好，龙头没了，你就当不上大官了，并说破解的办法，只能是立个华表，于是就出现了这个麻烦。<br />
<br />
薄不怕Jiang Zemin看见，他自有办法。他估计Jiang Zemin在华表附近要下车巡看，就派了9个政府官员，装扮成普通市民，背好了台词，事先久候在这里，他们己排练了4次，连腔调与动作等细节，都经过领导仔细审查通过。果然，Jiang Zemin一下汽车，这些闲逛的市民，立即围拢了过去，又是鼓掌，又是喊叫，纷纷大力颂扬薄市长。有一个妇女告诉江主席，她是下岗工人，被政府认真安排了新职业，具体办理的部门是大连劳动局下属的戚秀玉职业介绍所……这都是俺薄市长抓的紧呀！这话使Jiang Zemin乐坏了。笑满象个老太太。<br />
<br />
但他做梦也不会想到，这个女人是大连西岗区政府的一个副科长，何谈下岗？这一番表白，攒了500元表演费。<br />
<br />
在高大的华表前，薄熙来趋着Jiang Zemin高兴，说，过去的皇帝到处走，处处都有行宫，所以大连也应当有华表呀！对，Jiang Zemin听了更乐了，大嘴巴恰似喝水的赖蛤蟆。<br />
<br />
在小姨子家门前专门开了一条路<br />
<br />
说也凑巧，Jiang Zemin的小姨子王某某，原在大连海运学院当教师，后来该院改成大连海事大学，她的工作变化不大。俗话讲，一人得势，鸡犬升天。以前薄市长常派车克民给她送礼，又给她安排了舒服轻松的工作，但探得其与姐姐王冶平来往很少，故以前也就不曾在她身上花太多工夫，这次不同，Jiang Zemin堤出要到小姨子家看看，薄熙来哪敢怠慢。<br />
<br />
在此之前，薄下令相关部门，在大连市沙河口区同泰街购买了由万达房地产公司建的精品小区里一套样品房，给王某某搞了 “乔迁之喜”，薄还曾亲自赶去“温锅”，并赞助了全套家具电器，然而，薄还觉得做的不够，就索性在此搂前，修建了大草坪与小花坛，还为协助其出行，修了一条呈对角线的方砖步道。薄熙来对铁哥们讲，胜败在于细节，对这个女人要无。微不至地关怀，因为我们的命运捏在他姐夫手里！<br />
<br />
据大连参与警卫工作的人士披露，王冶平去见妹妹时，因为Jiang Zemin要与她们讲私房话，所以薄熙来等人不能陪同，就在楼下的汽车上等候，一等大半天。历来十分重视自已时间的薄熙来，这时全力以赴，所有的正常工作都交给了助手，等Jiang Zemin等得通霄达旦，日夜不眠。<br />
<br />
接近薄市长的人表示，为了知道Jiang Zemin与人接触时的讲话，薄熙来斗胆让大连国安局启用了从西德进口的一种监听设备，由国安特工车克民驾车与其使用，这个东西在距离上述住宅百米之内，可以听到他们所有的谈话。在那段时间，薄用这个仪器把Jiang Zemin玩于股掌之上，而他们毫不察觉，还以为薄熙来是忠臣。<br />
<br />
花重金把Jiang Zemin孙子拉下水<br />
<br />
这回Jiang Zemin不仅携妻同行，而且还带上了他最心疼的孙子，在来大连之前，江的秘书贾廷安等人就把这一消息，通报了薄熙来。他叫车克民等人早已把江的美籍孙子的自然情况，摸的一清二楚。比如他喜欢足球，崇拜球员李明与教练李应发等人，还有他正巧此间过生日，又喜欢系朱红色领带，吃海鲜，等等。<br />
<br />
于是薄熙来进行了精心运作，他使出全身解数，陪好江的孙子。在有些关于讧泽民访问大连的公开报道中，设有出现薄的名子，这里的原因就是薄把更多的精力，用在了刀刃上。薄对下属说，江主席最心疼的人是小孙子，我们把他搞定了，一切都顺理成章了！<br />
<br />
所以，薄亲自陪同Jiang Zemin与其孙子参观了大连实德足球队，并与主教练李应发与球星李明一同合影，这使戴眼镜的Jiang Zemin孙子，乐开了怀。照像前，薄还精心为这两个人选择了一样的朱红色领带，一样的深蓝色的西装……真是处心极虑，细致入微，极尽拍马溜须之势。<br />
<br />
在此期间，薄熙来准备了一些部门，精心布置好了人员，准备请Jiang Zemin去看，但江主席却想自已做主，所以也常常临时变动参观行程，薄对此很是恐慌，因为他知道，在虚假经济繁荣的背后，还有许多阴暗面，不希望Jiang Zemin看，所以必须笼络江的孙子，叫孙子左右爷爷。薄命令大连新某房地产集团的老板孙某某，准备了10几张银联卡，不仅向Jiang Zemin的随员多人行贿，而且还当众公开赂贿Jiang Zemin的孙子。一位大连新闻界的消息人士说，他亲眼看见大连一个官员给了他一张银行卡，上面存了多少钱不得而知。另一个接近随行人员的人说，大连之行薄安排下面跑腿的，分发了许多这种银联卡，每张多少钱，大慨不会少于8万吧。<br />
<br />
目击者说，在参观了大连造船新厂与戚秀玉职业介绍所以及华录松下电子信息有限公司之后，Jiang Zemin已身心疲倦，但薄还想让江再去大连商品交易所参观，自已不便强求领导，就事先赂贿收买江的孙子，叫他去拉爷爷，这样薄熙来最终达到了目的。之所以让Jiang Zemin参观这里，是为了告诉他，他很懂股票市场与资本运菅，当国务院总理没问题。<br />
<br />
据知情者描述，最可笑的一幕发生在大连游船上。为了给Jiang Zemin的孙子过生日，薄熙来选择了一条豪华的由军队使用的大船，供江家享用，不仅准备了生猛海鲜飞禽走兽，而且还请来了著名京剧表演艺术家杨赤等社会名流，边吃边唱，众星捧月。当然还有服装模特当服务员，为大家助兴。席间满脸奸笑的薄熙来等地方官员，又是切生日蛋糕，又是唱祝寿歌，又是吹蜡烛，。又是拍手呼叫，仿佛这不是在面对一个在美国读书暑假回国探亲的不满 20岁的学生，而是他自已的亲爹。有一个参与其事的干部对我说，这真是有奶就是娘，薄熙来为了往上爬，连个小孩也能叫爹！<br />
<br />
另据透露，当天吃喝玩乐，得意忘形，不料出了问题。半夜里Jiang Zemin一家全部腹泄拉肚子，吓的警卫人员与医生四处奔波。由于薄熙来等人平安无事，有人怀疑是他有意捣鬼，查了半天也无证据。原来，由于多年来薄熙来以牺牲大连的自然环境为代价，大搞经济，把大量油污废物倾入大海，造成海洋生物中毒，而当地人天长日久，或具备了免疫力，或癌症身亡，已见怪不怪。只有外地来的人，吃了海鲜必将跑肚拉稀。贪食的Jiang Zemin一连两天跑厕所，把腰带都拉细了，而孙子才拉了两次，服了两片立特灵与黄莲素就好了。当地一个下岗工人说，妈的，怎么不把这个蛤蟆嘴拉死！<br />
<br />
这个有关Jiang Zemin大连之行的故事，到至今还在大连人的饭前茶后流传。2007年2月13日，当地新闻界一位资深记者对我表示，Jiang Zemin晚了一步，要是在中共16大会议上，就叫薄市长进入中共中央政治局的话，现在他就取代了Hu Jintao，当上了总书记，那么，大连市中山路上挂的画像就不再是Jiang Zemin了，而是俺们薄熙来！<br />
<br />
2009年8月14日于多伦多<br />
