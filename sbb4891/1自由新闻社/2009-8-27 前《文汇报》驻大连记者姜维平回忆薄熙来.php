																											<div id="postmessage_218159" class="t_msgfont">2009年8月27日 星期四 <br />
&nbsp;&nbsp;<br />
Jiang Zemin曾庆红考虑挺薄熙来 <br />
&nbsp;&nbsp;<br />
姜平 <br />
&nbsp;&nbsp;<br />
【人民报消息】8月22日至25日，Hu Jintao在新疆考察工作。8月22日至24日，Wen Jiabao在浙江考察。8月22日至25日，中共中央政治局常委、国家副主席Xi Jinping在内蒙古自治区调研。8月23日至25日，中共中央政治局常委、国务院副总理Li Keqiang在青海考察。<br />
在此期间，薄熙来在新华网和人民网非常活跃，上面出现了很多吹捧他的消息，人民网甚至还有一篇文章《《薄熙来打黑 汪洋欣慰》。<br />
<br />
记的过去Hu Jintao一出访，Jiang Zemin就在这两个网上活跃几天，发表自己喜欢发表的文章，等胡一回来江又消失了。此次看看，薄熙来也如此。这说明薄的势力还不足以对抗这些人。<br />
<br />
薄熙来有很多地方的做法和江很相像，思路都相同，所以薄很能揣摩到如何让江高兴、如何让江上钩。薄熙来从来没有崇拜过谁，也没有孝忠过谁，他所做的一切事情都是为了扫清仕途上的障碍，达到权力的顶峰。这里可以举几个小例子。<br />
<br />
● Jiang Zemin和薄熙来都对亲爹翻脸不认<br />
<br />
Jiang Zemin和薄熙来都有对亲爹翻脸不认的故事。<br />
<br />
Jiang Zemin出身汉奸，为了达到往上爬的目地，在出身上下的功夫之大，是罕见的。江上青是江的叔叔，被土匪乱枪打死，中共称其是「烈士」，为了把档案里出身「汉奸」换成「烈士」，江首先要去江上青遗孀、也就是比江大15岁的婶婶王者兰那里搞好关系，常常送东西，并拢络两个堂妹，软磨硬泡，最后得到通行证。后来江顶着「江上青过继子」的名义，骗过了张爱萍和汪道涵，这两个人都是帮助Jiang Zemin达到仕途顶端的重要人物。而别说那个汉奸亲爹江连理也不理，就是亲兄弟姐妹，江也不来往。而假爸爸江上青的两个女儿江泽玲和江泽慧，江却百般讨他们的欢心。<br />
<br />
堂妹江泽慧原不过是安徽农业大学一名普通教师。因为Jiang Zemin升官靠的是履历表填人家那「烈士」爸爸，所以为了堵住江上青女儿的嘴，江泽慧受到火箭式提拔，先升安徽农大林学院院长，随即再升农大校长，然后又任中国林科院院长，成了部级干部，出入人大会堂走「党和国家领导人」通道。安徽省副省长王怀忠被处决前，曾坦白他光在Jiang Zemin的「胞妹」江泽慧身上就花了不下四百五十余万元人民币的代价。 <br />
<br />
薄熙来的老爹薄一波在文革时被打倒，其中一个使力的人就是亲儿子薄熙来。家里几个兄弟姐妹一直都对他这种做法耿耿于怀，说他太自私，为了保住自己，竟对亲父亲下手。<br />
<br />
江的汉奸父亲的成份是不可能更改的，但Hu Yaobang给薄一波平了反，薄熙来又成了太子党，薄一波又成为可以被薄熙来利用的工具。2007年1月，在十七大召开前几个月，薄一波咽了气，准备利用他的资格给自己往国务院副总理位子上推的儿子薄熙来，这回真是哭的上气不接下气，他埋怨薄一波为何不在十七大召开之后死。<br />
<br />
<br />
<br />
● Jiang Zemin和曾庆红考虑挺薄<br />
<br />
Jiang Zemin当政时，并不想把薄熙来提拔到至关重要的位置上去，主要原因有二，一是薄一波资历比江老的多，即使提拔了薄熙来，他也不会像其他平民出身的人那样感激涕零，成为自己的死党；二是江身边的人都替薄熙来讲好话，江就知道一定是得到好处了，因为江自己过去就是这么做的。<br />
<br />
薄一波活着时，曾在Hu Jintao、曾庆红去看望时，提出让儿子当国务院副总理，但未如愿。薄一波死后，薄熙来被分配去重庆市，于是他拎着重礼去「看望」Jiang Zemin和曾庆红，请求他们帮忙说情，如果不能当国务院副总理，那宁可回辽宁省，也不去重庆市。但礼物送了，依然没有下文。<br />
<br />
今年9月要召开十七大四中全会，薄熙来再次感到，只在宣传口下功夫还不行，那不过是造舆论，在高层必须有人在关键时刻替他说话才行。于是在重庆一再遭灾时，薄熙来没有一次出面，原因是正忙于在北京四处活动。薄又一次砸下重金，厚着脸皮去找曾庆红，请他为自己「作主」，说到无奈处，薄熙来哭了。曾庆红答应他，「尽力而为」。 <br />
<br />
Jiang Zemin最近感到形势对自己很严峻，也答应薄熙来「会考虑帮忙」。<br />
<br />
薄熙来信心满满的对亲信说，江和曾庆红都认为，今后能对付胡温的人也就数他了，Xi Jinping不行。<br />
<br />
中共十七大四中全会将有比十六大四中全会更激烈的较量。△</div>

							
							
							
							
