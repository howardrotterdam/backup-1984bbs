													<h2>王力雄：新疆追记</h2>
						
						
																											<div id="postmessage_150288" class="t_msgfont">　　中共曾经针对新疆问题发过一个著名的“七号文件”，其中有这样一个关键的定性――“影响新疆稳定的主要危险是分裂主义势力和非法宗教活动”。这句话在句式上模仿当年毛泽东所说“新疆的主要危险是苏联现代修正主义”，只是把矛头从外部的国际关系转向了内部的民族关系，成为近年中共治理新疆的指导思想和政策基础。<br />
<br />
　　治理新疆的思路变化，有其复杂的演变，不能一言论定。然而明显的一点是，如果把新疆的主要矛盾定为“分裂主义势力和非法宗教活动”，就等于先天地把生活在新疆的汉族和当地民族分成两个集团，并且使他们之间互相对立起来。因为无论是“分裂主义势力”还是“非法宗教活动”，都是针对当地民族的。首先汉族肯定不会要分裂，其次汉人很少信仰宗教，尤其不信当地民族的伊斯兰教。所以把分裂和宗教问题定为新疆的主要危险，就会导向一个必然的结果――汉族理所当然地成为依靠力量，而当地民族则成为需要警惕并加以看管的人群。于是就会发生所谓“预期的自我实现”――汉族把当地民族当作防范对象，当地民族最终就会真地被推倒敌对一方。在我看来，这才是目前新疆最大的和主要的危险所在。<br />
<br />
　　回顾半个世纪的历史，即使毛泽东时代曾对新疆当地民族进行过残酷镇压，但由于那时在意识形态上成功地以“阶级”取代了“民族”，新疆的民族关系也没有恶化到今天的情况。虽然不能证明当年广为流传的维族农民库尔班大叔骑毛驴去北京感谢毛泽东的故事是真，但是我的确亲耳听过老辈维吾尔人讲述当年的民族关系和他们对汉人的好感。六十年代在乌鲁木齐，公共汽车上的汉人见到少数民族会主动让座，少数民族去北京时更是到处倍受欢迎。他们那时把汉人当成老大哥，认为汉人到新疆真是来帮助当地民族发展。然而这十多年的时间，民族关系的恶化速度惊人，程度也特别严重。尤其是在年轻人中间，汉族和当地民族隔阂越来越深，彼此越来越对抗。甚至在同一个机关宿舍大院里，小孩子都以民族分成不同团伙，互相之间只打架而不在一起玩。<br />
<br />
　　这种民族关系恶化主要是由汉人引起的。汉人总是以统治者的眼光看待当地民族。非我族类，其心必异。<strong>1997年新疆出过爆炸事件后，公共场所要求检查来者随身带的包，对汉人只是马马虎虎地看一眼，纯属走形式，对当地民族的人却检查得非常仔细。这种明显的民族歧视很快蔓延到整个中国，无论在哪个城市，看见新疆少数民族模样的人，警察动辄就要拦住盘问，出租车司机不给停车，店家也特殊防范。</strong><br />
<br />
　　<strong>一位乌兹别克族的教授讲他去上海的经历，那天深夜才下火车，正赶上大雨滂沱，雷电交加，他跑进车站附近一家私人小旅店，已经全身湿透。店主是个里弄老太太，只因为看他长著新疆人的面孔就拒绝他住，说是按照市政府规定，新疆人只能去一家指定的回民旅店。他当时实在按奈不住，大发雷霆，说你们上海当年来新疆几十万人，我们收下了，给你们吃和住，什么都不要，今天我到你们上海住一夜，我是给钱的，你们都不让，你们上海还有没有一点良心？他发誓从此再不去上海！</strong><br />
<br />
　　中国内地城市对新疆人的歧视伤了很多新疆人的心，仅这一件事就足以把很多新疆人推到汉人对立面。既然你们像防贼一样防我们，我们为什么还要在一个国家？罪犯哪里都有，怎么能因为新疆出了恐怖分子就把所有新疆人都当成罪犯。汉人犯罪的更多，为什么不对汉人采取那些对新疆人采取的措施？西方社会有种族歧视，但要隐蔽得多，至少不敢像上海那样赤裸裸地实行种族隔离，然而都可以让那么多去过西方的中国人变成民族主义者，新疆当地民族的人为此憎恨汉人一点也不难理解。<br />
<br />
　　原来新疆民族的知识分子普遍不怎么去清真寺，自从把“非法宗教活动”定为新疆的主要危险并且进行打压后，反而越来越多的知识分子开始去寺院。政府在伊斯兰的斋月发布文件，禁止学校老师和学生封斋，然而封斋的人一定会比不发文件之前增加很多。原本那些“民考汉”（即少数民族学生去汉语教课的学校上学，并用汉语参加考试和升学）的家庭，现在又让自己的孩子去上本民族学校，同时开始为自己汉语比本民族语言好而感到羞愧（过去会为此感到自豪）。今日新疆，“逆反”一词总是能最充分地得到体现。<br />
<br />
　　新疆汉人和当地民族的不认同，一个典型例子是在时间的使用上。<strong>新疆的地理位置和北京相差两个时区，新疆人民代表大会曾为此通过法令，在新疆实行乌鲁木齐时间。然而去看当地人的表就会发现，新疆汉人的表一定都是北京时间，而当地民族的表则一定都是乌鲁木齐时间。所以在新疆约时间，一定要视对方的民族身份来认定约的是什么时间。当地民族与汉人约时间，双方也必须先说清到底是北京时间还是乌鲁木齐时间。这种区别反映出双方的取向，汉人更愿意和北京保持一致，不把当地法令放在眼里，当地民族则坚持强调自己与北京的不同。</strong><br />
<br />
　　汉人总是自觉不自觉把自己摆在镇压者的位置，就连兵团那些临时从内地农村招来的农工，对镇压当地民族的任务都兴致高昂，摩拳擦掌地请战。<strong>中国内地大量发生的民事纠纷或刑事案件，若是发生在新疆，往往就会被那些企图从一切事物中发现“不稳定萌芽”的人政治化，提升处理的层次，导致事情越弄越大，最后会使普通的刑事案件变成政治案件。</strong>民族之间原本没有那么大隔阂，就是因为不停地念叨分裂，结果就会真地开始发生分裂。<strong>新疆本地民族把三四十年代统治新疆的汉人盛世才视为刽子手，从而把在新疆实行强硬政策的中共书记王乐泉称为王世才。然而乌鲁木齐一位汉人出租车司机看见我手拿一本刚从书店买的《塞外霸主盛世才》，立刻热情地表达对盛世才的敬佩，告诉我“那时的政策才好”。新疆当地民族对屠杀过大量本地人的王震恨之入骨，新疆汉人却对王震崇拜有加。</strong>这种彼此完全相反的认识眼下似乎没有多大影响，然而在历史观点上的对立从来就是冲突与分裂最深处的根源。它表现的是民族之间人心的分离，比别的分离都更为本质。<br />
<br />
　　目前中国对边疆少数民族地区（西藏和新疆）的治理表面上看行之有效，控制有力，局势稳定。两个“封疆大吏”――新疆的王乐泉和西藏的陈奎元（刚刚调任）所用手法如出一辙，遥相呼应，受到北京嘉许。然而其征结所在是日益失去当地民族的人心。失去人心的稳定只能是维持一时的稳定，是以失去长远稳定为代价的饮鸩止渴。所谓“失人心者失天下”，今天的表面稳定正在为未来的冲突埋设炸药。尤其对新疆，未来的冲突可能会非常暴烈。宗教的不同使新疆当地民族与汉族的对立比西藏更为激烈，继续按照今日中共的道路把新疆民族关系向敌对方向推下去，用不了多久，就会彻底丧失掉重新转为良性互动的可能，只能恶性互动不断激化矛盾，把双方越推越远，变成不可挽回。那样一种危险的前景，目前已经在边缘，而一旦进入那种不可逆的进程，新疆就可能成为下一个中东或车臣。<br />
<br />
　　<strong>一位维族青年的话一直让我无法忘怀。当我问他想不想去麦加朝圣的时候，他回答那是他的梦寐以求，但是他现在不能去，因为可兰经中有这样的教导，当敌人还在占领家园的时候，不能去麦加朝圣。</strong>他没有把话说下去，但是已经不言而喻。为了他梦寐以求的愿望，他一定会不遗余力地为把汉人赶出新疆而战斗。<br />
<br />
　　<strong>而另一些汉族知识分子――包括北京一些最高层次的知识精英――则更让我感到震惊，平时他们都是一副改革、开明和理性的形象，然而一谈到新疆问题，从他们嘴里竟可以那样轻易地迸出一连串的“杀”字。</strong>如果靠种族灭绝就能够保住中国对新疆的主权，我想他们可能会眼看著几百万维吾尔人被杀光而不动声色。<br />
<br />
　　当下北京解决民族问题的思路，除了强硬镇压，另一手就是发展经济。按照他们的想法，只要发展了经济，少数民族的生活水平得到提高，民族分裂就会没有市场，宗教影响也会被世俗化消减，民族问题就会自然而然地解决。这两年轰轰烈烈掀起的“西部大开发”，相当程度就是出于这种思路。1994年以来，北京一方面在新疆民族问题上采取强硬路线，一方面大幅度增加给新疆的财政拨款（1994年29.5亿元，1995年52.3亿元，1997年68.4亿元，1997年是1994年的2.3倍）。从绝对数上看，北京给新疆的拨款比给西藏的拨款（1997年为34亿元）多一倍，是全国各省市区得到中央拨款最多的。<br />
<br />
　　无论从其公布的数字，还是在新疆的实地感受，都能看出近年新疆经济的快速发展。然而民族问题却没有如北京希望的那样变小，当地民族的人心仍在渐行渐远。以经济发展来稳定新疆的思路所犯的一个基本错误就是，民族问题的本质并非是经济的而是政治的，企图在经济领域解决政治问题，本身已经是一种倒错，何况政治高压还在继续不断地加强，民族问题怎么可能指望得到解决？<br />
<br />
　　退一步，即使仅从经济角度谈，只要政治问题不解决，资金的投入和经济开发的规模无论达到多高程度，都不一定能够取得效果，甚至效果会相反。例如北京一直标榜给了新疆多少钱，可是当地民族马上会反问新疆被抽走了多少石油，那些石油的价值是多少？<strong>被列为“西部大开发”的第一号工程是“西气东输”――即是把新疆的天然气采出后输送到中国内地。新疆人对此质疑究竟是开发西部还是掠夺西部，不能说是没有理由的。只要人心保持对立，民族之间互不信任，经济上所做的一切――不管从北京的角度认为那是出于何等的好心――都免不了会被插上殖民主义的标签。</strong><br />
<br />
　　我相信北京高层的出发点会是真心希望通过“西部大开发”缩小少数民族与汉族之间的经济差距，改善少数民族的生活状况，使少数民族在这个过程中得到比汉族更多的好处，从而减少少数民族的离心力。然而在具体实施过程中，结果一定是反过来的，汉族无疑将拿走最多的好处，少数民族与汉族的差距更加扩大，少数民族的不满因此只能进一步加强。北京拿出大笔的钱，最终得到的结果可能只是竹篮打水一场空，甚至比原来更糟。<br />
<br />
　　之所以我敢这样断言，一是新疆有660万汉人，占新疆总人口的近4成。新疆权力资源、经济资源和知识资源的大头都掌握在他们手中，他们有足够的能量在任何一次新分配和新机遇到来时攫取超过当地民族的利益。举一个最简单的方面为例，新疆的经济和中国内地结为共同体，完全依赖中国内地，那么仅一个汉语的使用，就使当地民族处于极大的劣势。<strong>不要说那些与中国内地做生意的项目，就是找新疆本地的工作，汉话说得不好或不认识汉文都会在很多场合成为被淘汰出局的第一理由。在今日新疆，凡是在高层次的工作环境，会看到大部分工作人员是汉族，而底层的工作环境，如煤矿、砖窑、水泥厂的车间等，主要人员都是少数民族。</strong><br />
<br />
　　<strong>我认识一位搞语言学的朋友，不久前做的一个科研课题是设计少数民族汉语考试的分级。那是要让汉语考级如同英语考级一样规范化，还要颁发级别证书。他向我征求意见时，我说他的设计如果推行，将来少数民族找工作就得拿著汉语级别证书，门槛就会更高。他认为那是经济发展的必然，我请他设想一下如果我们汉人在中国找工作都得取决于日语水平考级，我们会是什么感觉？</strong>少数民族在这方面是一样的心理，可不是仅仅用经济二字就能包容的。<br />
<br />
　　<strong>新疆的失业很严重，尤其是少数民族的青年，经常找不到工作。新疆的汉族青年至少还有一个内地可去闯荡，少数民族青年只能在家呆著。我在新疆旅行时，经常能看到城镇和村庄街边到处是少数民族青年成群而聚，闲聊或是打闹。看著那种前景，你不由得会产生一种恐惧，一个社会有这么多青年无事可做，不能把浑身精力升华释放的时候，另一方面却在不断地积累仇恨，社会最终会发生什么样的危险，实在是很难预料。</strong><br />
<br />
　　不过这里的确有经济自身的规律在发生作用。如工程招标肯定只能给最符合标的的公司，而不会考虑公司老板是什么民族。任何经营者首要追求的都是利润和效率，而不是公正和平等。既然能招到讲汉语的职工，为什么要去招只会讲维语的职工，然后还得给他们配备翻译？新疆不少农村有这样的情况，当地民族的农民嫌种地辛苦却得不到多少收入，税费又重，宁愿把自己的地转包给内地来的汉族流民。汉族流民善农耕，有市场意识，种同样的地，所得不仅能够交足当地政府的税费，自己还有可供发展的利润。当地政府自然欢迎这样的流民，他们给当地经济带来活力，也给地方财政带来收入。随著时间，这种流民的经营规模逐步变大，成为地主，遇到问题和纠纷也有钱进行摆平，建立起自己的庇护网络，他们也就在当地长期扎根和发展下去了。有些看法认为这是政府一种有计划的殖民，是汉人经济上对当地人的剥削掠夺，不能说完全没有这样的成分，但是在很多情况下，更重要的还是经济规律自发地在起作用。<br />
<br />
　　不管当前的“西部大开发”被赋予了多少美好的说法，在开发资源是由汉人掌握、而竞争能力又被汉人垄断的状况仍然是一种现实的时候，水往低处流，经济上的好处最终大部分被汉人所得是毫不奇怪的。当地民族只会被蛊惑人心的宣传吊起胃口，然后被现实的落差推入更大的失衡与不满之中。<br />
<br />
　　我和维族朋友在饭馆吃饭的时候，维族朋友对我说：“<strong>你看，在这种小饭馆里吃饭的，99％是维族，99％是自费，而去那些大饭店大吃大喝的，99％是汉族，99％是公费，这说明的是什么？！”</strong>少数民族的很多失落正是源于这种直观的对比。的确，在新疆的高档消费场所，很少看得到当地民族的人，呆在那里几乎就跟在内地城市一样，周围都是汉人，说的都是汉语。<br />
<br />
　　当然，肯定也会有一部分当地民族的人能从发展中得到好处，但他们不会是普通百姓，而是权贵，他们不是因为属于当地民族的一员而得到好处，而是因为属于权势集团的一员得到好处。他们只能是数量很小的少数。专制制度使权力在中国不受制衡，产生腐败是绝对的。从这个角度看，从所谓的“西部大开发”中得到好处最多的，只能是权贵阶层，底层百姓即使能顺便沾到一些光，然而布满他们视野的，只能是比原来更为扩大的两极分化，进一步衬出当权者的强取豪夺和自身的沉沦失落。那时，<strong>“阶级仇”和“民族恨”叠加在一起，“西部大开发”的结果可能会在西部产生更强烈的社会不满和民族敌对</strong>。<br />
<br />
　　我原来总是试图说服少数民族朋友，不应该把他们受的苦难看作是民族压迫，因为汉族也一样在受压迫。中国各民族的苦难根源都在专制制度，改善苦难的处境不是在各民族之间进行兄弟残杀，而是团结起来去改变社会制度，共同建设新的民族共同体。我对维族狱友说，并没有因为我是汉族，就改变了我们关在一起的命运（今天我还会举另一个例子――并没有因为轮子是汉人所搞，就避免了被镇压和取缔）。但我还是同意维族朋友的意见，如果说以前的年代各民族是受到共同迫害，90年代后对少数民族的压迫则明显超过了对汉族的压迫。而且在新疆，汉人往往与政府站在一起，成为对当地民族进行压迫的同盟。<br />
<br />
　　在这方面，新疆是最突出的。西藏虽然也有民族问题，但因为西藏的汉人比例小（西藏自治区1999年的汉族常住人口的人口比例为2.83％），形不成与当地民族对抗的群体，所以压迫主要体现为政治压迫而非民族压迫。新疆汉人占总人口比例将近四成，很多人都是以“非我族类，其心必异”的心态把自己摆在统治民族地位，政府也把汉人当作制约当地民族的力量。不同民族之间所受的信任和待遇明显不同，因此对当地民族而言就构成民族压迫。<br />
<br />
　　民族问题从政治压迫变成民族压迫，是一种危险的变化。因为如果面对的是政治压迫，只要对政治进行改变，压迫就可以解除，各民族团结建设共同的新社会也有可能。而如果少数民族认为他们不仅是受专制权力的政治压迫，而且是在受整个汉民族的压迫，政治的改变就不会让他们认为能解决问题，他们对出路的选择只能是民族独立，与汉族彻底分开，压迫才能最终解除。这对中国未来的社会转型是非常不利的，因为即使中国改变了政治制度，少数民族仍然不要留在中国，反而会借转型期国家控制力的衰弱加紧独立，那一方面会在中国缩小民主力量的发言权和生存空间，提供滋生法西斯主义和民族沙文主义的土壤，另一方面势必造成汉民族与少数民族之间的恶性互动，最终演化出暴力冲突和民族仇杀。<br />
<br />
　　不幸的是，新疆的情况正在向后者发展。为了对其专制统治赋予理由，中共把民族主义当作进行蛊惑煽动的精神工具。其掀起的民族主义浪潮在新疆的确起到了把汉人拉在中共一边的作用，但同时也在把当地民族推到了敌对的一边。那种敌对不仅是对中共的敌对，还是对整个汉族的敌对。这就是民族主义的双刃作用――得到它好处的同时也必然得到它的坏处。而坏处是长远的，最终会超过眼前得到的好处。我一直奇怪中共的一厢情愿，居然可以指望一个“中华民族”的人造概念把五十六个民族统一成一个民族，再以民族主义的鼓动使整个中国按照它的指挥棒一致对外。然而只要“中华民族”的概念不被其他少数民族认同（那是可以肯定的），被鼓动起来的民族主义就不会只充当政权的武器，而是每个民族都可以对其为我所用，都可以用民族主义煽动和凝聚自己的人民，并将其作为追求少数民族分离和独立的根据。<br />
<br />
　　我和同牢的维族青年E成了兄弟一般的朋友。我们经常谈的话题说起来会让人觉得可笑，虽然我们都是身陷囹圄的犯人，可是却在扮演各自民族的代表，进行的是未来怎样解决新疆问题的谈判。“谈判”一般背著别的犯人进行，有时我们会在狭窄如笼的寒冷小院并肩踱步，一谈就是几小时，只有当武警持枪的身影笼罩在头顶时才陷入沉默。我们不是把“谈判”当成打发时间的游戏，非常认真，也有争论。例如我强调搞极端主义和恐怖主义最终一定伤害到维吾尔人自己。我以达赖喇嘛为例，一时看他的非暴力主义不如恐怖主义影响大，但今天那么多汉人迷恋西藏文化，信仰西藏宗教，汉地出版了那么多有关西藏的书，西藏成了汉人精英的时尚，这一切是与整个藏民族奉行达赖喇嘛的和平主义分不开的。在这种背景下，达赖喇嘛关于解决西藏问题的政治主张也逐步开始得到汉人的理解。相比之下，只要是跟穆斯林有关的题材，汉地出版社一概不敢碰，因为以往的教训使他们知道，稍有不慎就会引起极端反应，造成事件。今天，维吾尔民族到底是怎样的情况，维吾尔人到底是怎么想，汉人几乎完全不了解，这与上述文化沟通的中断不能说没有关系。汉人轻易不敢去新疆城市以外的地区，连我这样习惯闯荡的人!也不敢独自在维族地区旅行。这或许是出于一种误解，但正是因为新疆发生过的恐怖活动和激烈行为，造成了汉人认为维吾尔人普遍具有攻击性的看法。两个民族之间的隔阂越来越深，对此汉族固然应该负主要责任，维吾尔族的精英也需要自省。我在海外遇到流亡维吾尔人，他们在对话前的第一个要求就是不能说“新疆”两个字，只能称“东土耳其斯坦”，否则免谈。而那种要求等于是彻底关掉与汉人对话的大门，因为哪怕从习惯而言，也不可能让人把用了几十年的称呼顿时换成另一个，何况汉人对此还有认识上的巨大差异。面对差异，不应该立刻要求论定是非，不妨给互相之间一个理解和变化的时间。如果你就是不给他时间，汉人也不会求著你给，他也干脆不理你了，那难道对维吾尔人有利吗？毕竟在实力上是汉族强维族弱，弱小民族更有利的武器因此应该是道义而不是强力，是对对方人心的争取而不是一概而论的仇视与排斥。从这个角度看，达赖喇嘛的和平主义远比暴烈的恐怖主义更有力量。<br />
<br />
　　E也批评汉人的国家主义。为什么你们汉人提起国家统一就认为是不可怀疑的公理？为什么汉人可以有自己的国家，别的民族就不能有？你们反对专制，追求民主，但是少数民族的民主你们是否尊重？如果少数民族的民主就是要求独立，你们是用民主的方式对待，还是用专制的手段解决？汉族精英动不动就用国家需要来为统一辩护，什么台湾岛链是中国通往太平洋的门户，西藏高原是中国腹地的屏障，新疆是中国未来的生存空间……然而国家是什么，国家只是一个概念，真实的是组成国家的人。也就是说，你们汉人为了满足自己的需要，不考虑少数民族的需要，或者干脆牺牲少数民族的需要，这是否就是你们要求保持统一的实质所在？<br />
<br />
　　我接受E对国家主义的批评，我原来对民族问题的思考也没有完全抹掉国家主义色彩，这次新疆入狱的经历让我开始对此反省和清除。我表示，国家的概念应该退后，民族的概念也同样应该退后，因为民族和国家一样是抽象概念，真实的存在只是具体的人。对我们来讲，重要的不是国家，也不是民族，而是现实中活生生的一个个母亲、妻子、丈夫和儿女，怎样能够让他们每个人都免于恐惧和匮乏，获得自由和尊严，才应该是我们考虑一切问题的出发点。从这个角度，统一不是目标，独立也同样不是目标。如果维族和汉族能够和平地分离，各自追求适合自己的生活，我不反对，问题是能不能够做到这一点？如果不能的话，如果分离一定带来冲突和流血，造成战争、恐惧和匮乏，我们就应该考虑是否可以在不分离的情况下，同样使各族人民获得自由和尊严，那就成为比独立更好的选择。<br />
<br />
