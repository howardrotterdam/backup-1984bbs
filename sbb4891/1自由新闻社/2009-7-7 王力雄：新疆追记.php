　　新疆是这样一个地方，维吾尔人800多万，汉人近660万，哈萨克族130多万，回族80万，柯尔克孜族和蒙古族各十几万，民族混杂，互相制约。从各自的背景来看，虽然新疆汉族的背后有十多亿人的中国，后援强大，但当地民族的后援也不可小觑。如当地7个民族信仰伊斯兰教，而新疆接壤的8个国家（新疆是中国接壤国家最多的省区）有5个是伊斯兰国家，那些国家又通往中亚、西亚和阿拉伯等更广大的伊斯兰世界；当地5个民族属于突厥语民族，毗邻的伊斯兰国家也多属于突厥民族，他们的背后又有土耳其那样强大而野心勃勃的“世界突厥人祖国”；新疆还有6个当地民族是跨界民族，且毗邻国家就是他们的民族国家，如哈萨克、塔吉克、蒙古、俄罗斯等。新疆当地民族很清楚只靠自己对付不了中国，因此从来都在世界一盘棋中考虑问题。当我听到新疆少数民族人士如数家珍地谈论周边国家、伊斯兰世界和国际社会时，经常为他们的广阔眼界感叹不已，自愧不如。我相信汉人未来在新疆面对的，一定不仅仅是当地民族，而是变量众多、错综复杂的力量。汉人搞定新疆绝非轻易之事。<br />
<br />
　　安全厅看守所里有一个维族犯人，其他犯人私下里都叫他“国防部长”。我没见过“国防部长”，但一进看守所就开始听人讲他，他已经被传成了一个传奇人物。他是在参加车臣独立战争时被俄军俘获引渡回中国的。他之所以去为车臣打仗，是因为他们都是穆斯林，打的是圣战。那么将来维吾尔人若是打起了脱离汉人异教徒统治的圣战，其他国家的穆斯林――那些剽悍的高加索人，善战的阿富汗人，富有的阿拉伯人……会不会也一样会投入呢？即使不考虑外力的介入，新疆本地的民族仇杀也会达到相当暴烈的程度。因为新疆的汉族和当地民族的力量基本势均力敌，当地的穆斯林民族的人口比汉人多（二者比例约为10:7），但是汉族掌握更多的资源，诸如武器、财富、技术和中枢位置等。如果双方不是势均力敌而是力量相差悬殊，冲突烈度反而可能会低。例如西藏汉人量少且不扎根，一有动乱迹象就会早早撤回内地，很少有人愿意留下坚守，种族仇杀的规模反而不会大。势均力敌却是最容易导致冲突升级的。新疆汉人一是人数众多；二是早在新疆扎根；三是集中聚居，尤其是在城市（如乌鲁木齐的汉人占到72.7％）和兵团――这决定了他们轻易不会撤离，即使是内地一时不能支援，他们也有保卫自己的力量。<br />
<br />
　　仇恨调动起来是没有止境的，仇杀一旦开始就会走向盲目和疯狂，残酷程度将难以想象。波黑的民族构成特征和新疆有相象之处，但波黑的人口规模只是新疆的三分之一，照样打了那么多年惨烈的战争，流了那么多血，震动了整个世界。波黑是新疆的前车之鉴。如果我们不及早地想法防止，未来有一天新疆成为更大规模的波黑，可能性完全存在。那时被夺走的生命可能达到十万百万，每个民族都会流很多血，各民族孤儿寡母的哭声将会震动新疆的大地。<br />
<br />
　　对那种前景，不仅是我担心，当地民族的有识之士也一样担心。在上海被拒绝住店的那位乌孜别克族教授对我说，中国将来肯定要出事，中国民主化之日，就是新疆血流成河之时，他一想起那种前景就害怕，因此他一定要把孩子送出国，不能让他们留在新疆。<br />
<br />
　　我和E用了很多时间讨论如何避免未来新疆发生流血冲突。让我感到欣慰的是E也认为追求独立是不现实的，同时对新疆当地民族也不见得有利。我们之间的共识是，新疆问题的出路是当地民族实现名副其实的自治。<br />
<br />
　　<strong>维吾尔人有一个政治笑话，说李鹏管理经济遇到了困难，去请赵紫阳复出继续为党工作，赵问让我担任什么，李说管经济的副总理，赵立刻拒绝说，我又不是维族！这笑话是讽刺不让维族掌握实权的状况。讲这笑话的维族朋友解释说，上海人到北京去当官你们都要骂“上海帮”，新疆的实权都被汉人把持，我们又怎么会满意？</strong><br />
<br />
　　<strong>其实不光官位多数被汉人所占，像新疆气象局那样的单位，一千多职工中维族只有三十多个；如果强调气象局是技术部门，需要懂专业，那么不需要专业人员的基层单位，如维族聚居的阿克苏地区，行署粮食处三百多职工中也同样只有二三十个维吾尔人……当然，用哪个民族的人只是自治含义里的最低条件。在西藏的各单位中，藏人干部职工经常是占多数的，但同样不能叫自治。即使民族地区的官员全部都由当地民族担任，并且掌握著实权，也不一定就是自治，因为关键要看那些官员是怎么产生的。如果都是由北京任命，自治就依然徒有虚名。</strong>只有官员是自下而上地产生于当地人民，自治才算开始具有真实的基础。<br />
<br />
