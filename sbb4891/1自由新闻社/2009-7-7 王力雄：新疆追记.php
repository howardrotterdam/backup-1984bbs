　　官场是这样，民间也有类似情况。乌鲁木齐的山西巷是维族区，那里的居民不执行政府的扫雪规定。家家户户只是把自己门前的雪往路中间扬，说路是共产党的地方，不归自己扫。每年临开春由军队进行“爱民劳动”，把路上一冬的积雪运走。一位新疆农业银行的维族干部告诉我，有些维族懒汉生活非常贫困，但是政府拨款为他盖了房子，他却立刻又会把房子拆掉，为的是卖木料换酒喝。新疆农村有相当数量给个人的贷款发出去就再也收不回来。然而在我看来，这种现象的产生很大程度正是政府包办一切的结果，无论是个人的懒惰和无责任感，还是官场的懒惰和无责任感，根源都在这里。<br />
<br />
　　如果让当地人民进行自治，推举自己的政府，选择自己的决策，他们就会是而且只能是自己负起责任，出问题也要由自己承担，再也怨不著别人。这样一种结果，当地人民高兴，中央政府省事又省钱，何乐而不为呢？<br />
<br />
　　<strong>以一个例子说明。1991年我在拉萨，因为原有电力跟不上拉萨规模扩大和用电增加，几乎天天停电，人们怨声载道。当局那时决定在离拉萨不远的羊卓雍湖建设一座新电站。然而羊卓雍湖是藏人的神湖，藏人认为建设电站对神湖造成惊扰和破坏，反对的呼声很高。对此，当局认为自己做的是好事，藏人的所说的神湖是迷信，于是坚持把羊湖电站建成了，前后施工8年，花了18亿元。今天的拉萨再也没有停电问题，然而藏人一边享受著羊湖发电带来的光明，一边骂著花了钱搞建设的汉人统治者，民间还流传汉人借施工之名挖走了埋在羊湖下的宝贝，国际上也把羊湖电站当作北京不尊重西藏文化的实例。</strong>在这种问题上，为什么不能把思路变一变呢？何必由北京决定什么对藏人是好事或坏事？对于少数民族的选择，是迷信还是科学并非最重要，那是人家的文化，关键看人家愿意怎么样。如果让我考虑羊湖的问题，莫不如让拉萨人以投票方式选择究竟是愿意忍受缺电的不便，还是愿意建设电站。投票结果若是不赞成，那就省下18亿元；若是赞成票更多，反对意见也就没有理由再把矛头对准北京，而中央政府那时拿出18亿元建设电站，也就成为对藏人的真正帮助。<br />
