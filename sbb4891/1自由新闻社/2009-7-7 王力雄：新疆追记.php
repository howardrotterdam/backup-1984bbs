　　国际社会一直有关于北京正在消灭西藏文化的批评，流亡藏人更是以此作为对北京的主要控诉。然而传统文化的衰败是一个世界性问题，哪个民族都无法回避，包括汉族自己也不例外。西藏传统文化是否能抵抗得住全球现代化进程，即使是达赖喇嘛亲自统治西藏也会遇到同样的问题。那么北京何必要去自己背这种骂名，成为费力不讨好的挡箭牌呢？让西藏自治，让藏人去面对两难，进行选择，承受来自各方面的矛盾，自己旁观评论，岂不是藏人高兴，国际满意，自己减轻负担，又可以充当仲裁者？这才是对中央政府而言最聪明的做法。只要是国防和外交在中央政府手里，主权能够得到保证，少数民族地区无论怎样自治，都不会有大碍。<br />
<br />
　　有人可能担心资源的问题，真给了少数民族地区自治权，当地的资源――如新疆石油――国家是否就没有权力开采了。对此应该认定一个前提，即开采资源是需要对当地人民给予合理补偿的，而不是像现在这样打起国家旗号就可以无视一切。然而对资源补偿并不会使国家难以承受。现在国家每年给民族地方的拨款，在那时就可以转换成这种补偿。双方变成了明算帐。以后就不必一个说我无偿给了你多少钱，另一个说你无偿拿走了我多少东西。一切都按交易规则来做，那对双方的关系肯定是更有利的。至于国家和地方的权益怎么分配和保证，可以在历史基础上通过协商以法律确定。国家拥有武装力量，掌握著最大的资源，根本无需担心民族地方真正自治后会发生失控。只要严格遵守和执行法律，一切都不会脱离掌控，中央政府完全可以有这个自信。<br />
<br />
　　不过所谓自治还只是一个概念，到底什么是自治，具体内容是什么，在新疆怎么实行，都需要有十分深入的思考和设计。新疆实施自治的复杂性在于它是一个多民族混居地区，尤其是近七百万汉人怎样融和进新疆的自治中是一个大难题。新疆本地民族之间虽然也有诸多矛盾，但若不考虑汉人，他们之间共同的伊斯兰因素和突厥因素可以用来进行整合，然而有七百万汉人在新疆，强调伊斯兰和突厥因素就会导致以汉人为敌和排斥汉人，成为民族冲突的根源。<br />
<br />
　　我和E在这个问题上花的时间最多。监狱里有无数的时间，人的耐心因此特别强，可以不厌其烦地在每一个细节上反复质疑、讨论和推敲，并且乐此不疲。目前世界上那些现成的模式――西方民主制、政党政治、议会选举等，都不能应对新疆未来的局面，甚至会加剧矛盾。在这方面我们的看法一致，只有找到一条不同以往的新路，才能使新疆问题走出困境。<br />
<br />
　　E对我提出的“逐层递选制”表现出很好的理解和接受，这使我感到欣慰。和目前的其他自治概念都不同，逐层递选制是一种“全细胞自治”结构，它的自治是一层套一层的，自治之中还有自治。其实以往各种自治形式都非真正的自治，只是地方相对于中央的自我管理，对内仍然保持著统治结构。一般来讲，统治结构只能容纳一个统治集团，如果一个社会存在相互对立的两个阵营，不管哪个阵营把握了统治权力，都意味另一阵营是被统治的，因此这样的结构特别容易发生冲突。而在逐层递选的结构，任何一个单元、任何一级层块都是自治的，不存在一个自上而下的统治集团，也没有一方对另一方的压倒优势。各方因此比较容易保持宽容和平的心态。同时，这种自治也给不同族群的自行组合提供了可能。在每个自治体内部，相互都不用担心对方干扰，也无从发生冲突。每个自治体可以奉行自己的原则，流行自己的文化，保护自己的成员。而自治体之间的交往则是在逐层递选的更高层块上由当选者进行的。那种高层交往富于理性并善于妥协。直到时间提供了足够的过渡，造成异质与分裂的根源已经淡出，社会就会逐步进入到相互融合的状态。<br />
<br />
　　我无法在这里详细论述我们那些具体讨论。我当时尽可能地做了一些笔记。因为监狱当局经常搞突然袭击式的检查，为了防止笔记被他们当作“反动思想”的现行反映，我把笔记冠上了一个“古希腊城邦制度讲义（复习提纲）”的标题。看上去，似乎是我在默记过去上过的什么课，以及写下的一些心得。所用词汇都经过变形，谈的似乎是遥远的古希腊。<br />
<br />
　　在监狱里，我得到了很多新的想法，对新疆问题的认识有了新的角度，对当地民族也有了新的理解。从这个角度，这次坐牢倒成了一个收获。最重要的是，我觉得开始真正走进了维吾尔民族的内心世界。这在过去是从来没有做到的，即使在入狱之前，我专程来新疆结识维吾尔族朋友，自以为和他们有了很多交流，其实还是在他们之外。因为那时我还是以一个研究者的心态来看他们，把他们当作对象来观察。是监狱使我和他们的心贴到了一起，卷进了他们的共同命运，与他们承受起一样的苦难，和他们产生了血脉之间的融和。是的，自从我的血流在了新疆，新疆就不再是一个概念，当地民族也不再是隔岸相看的对象，而是成为我生命中不可分割的机体，会让我感到疼痛，让我感到目眩，也会让我感到情深意长，绵无绝期。<br />
<br />
