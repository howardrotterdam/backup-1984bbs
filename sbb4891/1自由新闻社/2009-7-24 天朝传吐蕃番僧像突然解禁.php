打印版 【 阿波罗新闻网2009-07-23讯】&nbsp;&nbsp;<br />
 　 中共当局一向禁止西藏寺院和藏人家中悬挂、供奉或私藏西藏政教领袖达赖喇嘛的法相，对违令者进行传唤、拘禁和判刑。但是近来在西藏东部康区甘孜州甘孜县呷拉乡境内，当局突然下令，要求当地藏人在各自寺院和家中供奉达赖喇嘛法相，并向藏人发放大型的达赖喇嘛法相，令所有藏人不解、惊讶，甚至产生怀疑。 <br />
据印度南部西藏哲蚌寺洛塞林甘孜僧人普巴引述境内最新可靠消息向本台表示，（录音）日前中共当局有关人士来到甘孜州甘孜县呷拉乡境内，声称之前不允许藏人拥有达赖喇嘛法相是极个别人的意思，政府方面对此没有禁止过，并指出藏人享有宗教信仰自由权。当局宣布，达赖喇嘛是佛教领袖，因此藏人信徒从此可以供奉达赖喇嘛的法相。随后当局还向藏人发放了大型的达赖喇嘛法相。<br />
<br />
中共政府之前严禁达赖喇嘛法相的政策突然出现180度大转变，令当地藏人感到极度惊讶，无法理解，并怀疑其中定有政治阴谋。截至目前，还没有藏人敢供奉达赖喇嘛的法相，害怕陷进当局设下的圈套。<br />
<br />
普巴表示，（录音）当地藏人之前因为偷偷供奉尊者达赖喇嘛的法相，遭遇过很多不幸，但是这次当局突然给予如此特殊的自由，使他们不得不怀疑，当局此举别有用心。有藏人说，这么多年来，尊者达赖喇嘛的法相在藏区被严重禁止，这次要藏人自由供奉，一定不怀好意。因此一直到今天，大家都不敢供奉尊者的法相。<br />
<br />
据了解，自1987年，西藏首都拉萨发生抗暴运动后，中共政府采取禁止全藏区供奉和私藏达赖喇嘛法相的措施，并作为治藏政策的一部分一直延续到现在。仅在四个月前，中共公安人员在西藏康区甘孜东谷境内，对每家每户进行大搜查，之后将装在共计18个袋子里的所有被搜查到的达赖喇嘛法相，在藏民前进行了焚烧。</div>

							
							
							
							
