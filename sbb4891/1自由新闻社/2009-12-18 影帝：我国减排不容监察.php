																											<div id="postmessage_381079" class="t_msgfont">温家宝称中国自主减排不容监察 <br />
<a href="http://www.chinapressusa.com/qihou/2009-12/17/content_268310.htm" target="_blank">http://www.chinapressusa.com/qihou/2009-12/17/content_268310.htm</a><br />
 【侨报网讯】 中国外交部副部长何亚非17日举行新闻发布会，向中外媒体通报了温家宝总理会见有关国家和国际组织领导人情况。温总理在会见时指出，哥本哈根联合国气候变化大会存在5个有待解决的主要问题。<br />
<br />
　　何亚非说，温家宝总理是带着诚心、信心和决心来到哥本哈根的，愿与各方一道，携手努力，推动谈判在最后时刻取得积极成果。温总理指出，现在会议到了最关键的时刻。各方应该立足于现实，充分展示政治意愿，照顾彼此关切，迅速凝聚共识，寻求一条有效推进谈判进程的道路，以推动会议取得成果。如果会议会无果而终，将严重损害国际社会合作应对气候变化的信心。<br />
<br />
　　温总理说，从大会和两个工作组讨论情况看，现在仍有五个主要问题有待解决:一是谈判的基础文件，二是减排目标，三是温室气体减排可测算、可报告、可核查－－－即“三可”问题，四是长期目标，五是资金问题。<br />
<br />
　　关于谈判的基础文件，温总理说，《联合国气候变化框架公约》和《京都议定书》两个特设工作组主席提出的新案文尽管不尽完善，但体现了各方在过去达成的共识，特别是坚持了公约和议定书的双轨制，符合巴厘路线图的授权，因而可以作为谈判的基础文件。需要强调的是，在公约和议定书下分别通过的“决定”要平衡同步，在表述上繁简一致。如果各方能以此为基础达成一项成果，将有利于为国际社会应对气候变化的合作和相关谈判奠定基础。<br />
<br />
　　关于减排目标，温总理说，目前，大多数发达国家已经提出了各自的量化减排指标，包括中国在内的一些发展中国家也提出了减缓排放的行动目标。虽然发达国家自身的减排力度不够，但是还对发展中国家的行动表示不满，甚至企图将两种不同性质的目标挂钩，这是毫无道理的。中方认为，各方应该从大局出发，锁定已做出的承诺，并且按照“共同但有区别的责任”原则，将各国承诺列入相关成果文件。如果能达成这样的安排，也将是一个大的进步。<br />
<br />
　　关于温室气体减排可测算、可报告、可核查的三可问题，温总理强调，“巴厘行动计划”对“三可”有明确规定。这些规定既体现了“共同但有区别的责任”原则，也是长期谈判的结果，是各方已有的共识。对于发展中国家来说，只有获得国际支持的减缓行动才接受“三可”审评，自主采取的行动则不接受国际“三可”评审。各方要实现各自目标，最重要的是采取切实行动，一打宣言不如一个行动。同时，增加各方之间的互信也是十分重要的，各方不应该相互猜忌，更不能搞对抗。<br />
<br />
　　关于长期目标和峰值问题，温总理说，应对气候变化既要着眼长远，更要立足当前，要把精力和重点放在完成近期和中期减排目标上，不能让长期目标上的分歧影响谈判进程。为体现诚意，中方可以考虑接受发达国家提出的“地球升温必须控制在2摄氏度以内”的目标是国际社会共同努力的方向。关于峰值问题，中方认为，可以在成果文件中表明国际社会应为全球排放尽可能早地达到峰值而共同努力。同时，为保持平衡，文件中也必须强调发展经济和消除贫困是发展中国家首要的优先任务。<br />
<br />
　　关于资金问题，温总理说，发达国家向发展中国家提供充足的、额外的、实质性的资金支持，是公约规定的法律义务，应该得到切实履行。衡量哥本哈根会议成功的最重要标志之一，就是发达国家能否拿出最大的诚意、尽最大的努力履行自己的义务。发达国家应该真正负起责任来，倾听发展中国家，特别是最不发达国家、小岛屿国家和非洲国家的合理诉求，以有力地承诺和行动来推动会议取得积极成果。在这方面，中方主要做了两方面的工作，一是通过各种场合和途径，敦促发达国家充分理解广大发展中国家特别是最不发达国家、小岛屿国家和非洲国家的处境和关切，切实履行自己应尽的的义务；二是尽管中国是一个发展中国家，仍然有1.5亿贫困人口，但是我们在南南合作和双边合作的框架内为最不发达国家、小岛屿国家和非洲国家提供了力所能及的帮助，以便提高他们应对气候变化的能力和手段。<br />
