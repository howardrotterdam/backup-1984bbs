													<h2>美营养师试验：麦当劳汉堡放置一年金身不坏</h2>
						
						
																											<div id="postmessage_525286" class="t_msgfont">麦当劳汉堡一年不坏 被指含防腐剂过多<br />
<br />
2010年03月21日13:50　来源：<br />
<img src="http://img.hexun.com/2010-03-21/123057872.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
相隔一年拍摄的同一份汉堡。<br />
<br />
　　据报道,美国营养师布鲁索拿麦当劳“快乐儿童餐”做了个试验,结果发现2009年购买的汉堡和薯条在一年后仍然完好无损,并指责麦当劳食品防腐剂太多。<br />
　　拍下照片的美国营养师布鲁索表示,这证明了麦当劳食物含太多防腐剂。她说:“食物最终应该要分解、腐坏并发出难闻臭味。<br />
<br />
　　62岁、有8名孙儿的布鲁索承认,她家靠近科罗拉多州丹佛落基山脉,气候干燥。但她补充,在为期一年的实验期间,她多次开窗,却没有苍蝇或其他昆虫被这些食物吸引。“如果苍蝇不吃开心乐园餐,(说明)微生物也无法分解它,那么孩子的身体也无法正常地将它代谢。现在你知道它为什么叫垃圾食物了吧。”<br />
<br />
　　记者向麦当劳中国公关部询问“这则消息是否属实”时,该公司有关负责人表示:“正在向有关方面了解情况”。而我国有关营养学家听到此消息大呼“惊讶”,觉得即便是在沙漠和冰箱冷藏室这样极端的环境,汉堡包等食物也不可能不变质腐败。<br />
<br />
　　美国营养师做实验<br />
　　麦当劳汉堡、薯条一年不腐败<br />
<br />
　　最早发布消息的是一家英国媒体,据报道拍下照片的美国营养师布鲁索表示,她把从麦当劳餐厅买回来的儿童餐打开,放在家里的架子上,一年之后拍摄的照片显示,除了汉堡牛肉排萎缩和面包坯干裂外,外形上看薯条和汉堡竟然没有明显变化。<br />
　　她说这证明了麦当劳食物含太多防腐剂。她说:“食物最终应该要分解、腐坏并发出难闻臭味。”62岁、有8名孙儿的布鲁索承认,她家靠近科罗拉多州丹佛落基山脉,气候干燥。<br />
　　她补充说,在为期一年的实验期间,她多次开窗,却没有苍蝇或其他昆虫被这些食物吸引。“如果苍蝇不吃开心乐园餐,(说明)微生物也无法分解它, 那么孩子的身体也无法正常地将它代谢。现在你知道它为什么叫垃圾食物了吧。”<br />
<br />
　　我国营养专家<br />
　　如果消息属实太不可思议<br />
<br />
　　昨天,中国营养学会秘书长贾健斌说:“如果消息属实,那太不可思议了!”他进步一说,汉堡含有动物蛋白和脂肪,如果腐败会分解出氨,而散发出臭味才对。即便是薯条属油炸食品,它也不可能一点水分也不含,油脂也会氧化分解,应该腐败变酸才对。<br />
<br />
　　对于美国营养师做实验的环境非常干燥的说法,贾健斌分析说,曾见到相关研究说食物在沙漠极端干燥的环境下会失水,可令保存时间变长久一些。但时间长些,细菌还是会繁殖,最终导致食物腐败。<br />
　　贾健斌表示,这是否意味着麦当劳汉堡和薯条防腐剂超标,还需要相关实验才能证明,很难发表看法。<br />
<br />
　　麦当劳中国<br />
　　正在核实消息源<br />
<br />
　　昨天下午,记者向麦当劳中国公关部询问“这则消息是否属实”时,该公司有关负责人表示,麦当劳在中国一直高度重视食品安全,所生产的产品均符合中国相关标准的要求,保证消费者安全。至于这些报道,则“正在向有关方面了解情况”。<br />
　　据知情人士透露,麦当劳中国公司注意到了网络上流传的汉堡试验,正在与美国麦当劳总部核实此事,看看视频中的情况是否属实,“针对这件事,下周可能会出一个正式公告。”<br />
<br />
　　新闻链接<br />
<br />
　　麦当劳曾承认<br />
　　有防腐剂但不超标<br />
<br />
　　此前,台湾麦当劳汉堡被查出含有防腐剂丙酸盐,当时麦当劳方面坦然承认,但是强调丙酸盐是符合当地卫生部门标准的防腐剂,丙酸盐的含量少于规范的标准。<br />
　　当时麦当劳称,每一个要卖出去的汉堡面包坯里面都有丙酸盐。并表示暂不会更改添加丙酸盐的配方,在台湾代工的面包坯厂商也会继续这样做下去。<br />
