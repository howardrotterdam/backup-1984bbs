<br />
领导人经常上网了解民意<br />
<br />
白皮书指出，互联网在政府与公众之间架起了直接沟通的桥梁。通过互联网了解民情、汇聚民智，成为中国政府执政为民、改进工作的新渠道，互联网上的公众言论正受到前所未有的关注。中国领导人经常上网了解公众意愿，有时直接在网上与网民交流，讨论国家大事，回答网民的问题。各级政府出台重大政策前，通过互联网征求意见已成为普遍做法。每年全国人民代表大会和中国人民政治协商会议期间，都通过互联网征求公众意见。近三年来，每年通过互联网征求到的建议多达几百万条，为完善政府工作提供了有益参考。</div>

							
							
							
							
