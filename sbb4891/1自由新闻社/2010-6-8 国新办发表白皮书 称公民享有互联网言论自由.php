<br />
&nbsp; &nbsp; 白皮书指出，中国政府大力倡导和积极推动互联网在中国的发展和广泛应用。随着互联网在中国的快速发展与普及，人们的生产、工作、学习和生活方式已经开始并将继续发生深刻的变化。目前中国已成为世界上互联网使用人口最多的国家。<br />
<br />
