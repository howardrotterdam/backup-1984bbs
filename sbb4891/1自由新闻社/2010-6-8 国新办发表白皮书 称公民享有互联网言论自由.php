　　互联网在中国新闻信息传播领域得到充分应用。中国政府鼓励和支持发展网络新闻传播事业，为人们提供了丰富的新闻信息，同时依法保障公民在互联网上的言论自由，保障公众的知情权、参与权、表达权和监督权。<br />
　　互联网成为人们获取新闻信息的重要途径。自从互联网进入中国，人们就充分运用互联网传播新闻信息。中国的通讯社、报社、广播电台、电视台等利用资源优势和品牌优势开展网络新闻传播，满足人们的新闻信息需求，已形成人民网、新华网、央视网、中国广播网等一批综合新闻信息服务网站，不仅扩大了权威新闻信息传播的广度，而且为传统媒体自身发展拓展了新的空间。一批著名的商业网站也成为人们获取新闻信息的重要渠道。据统计，80%以上的网民主要依靠互联网获取新闻信息。网络媒体的发展不仅提高了新闻传播的时效性、有效性，而且在报道重要新闻事件中发挥了独特作用，充分满足了人们的信息需求。网络媒体直播中国共产党全国代表大会、全国人民代表大会和中国人民政治协商会议等已成为惯例。<br />
　　公民依法享有互联网上充分的言论自由。《中华人民共和国宪法》赋予公民言论自由的权利。中国公民在互联网上的言论自由受法律保护，可以通过各种形式在网上发表言论。网上交流活跃是中国互联网发展的一大特点，论坛帖文、博客文章数量之巨大，在世界各国都是难以想象的。中国的网站十分注重为网民提供发表言论的服务，约80%的网站提供电子公告服务。中国现有上百万个论坛，2.2亿个博客用户，据抽样统计，每天人们通过论坛、新闻评论、博客等渠道发表的言论达300多万条，超过66%的中国网民经常在网上发表言论，就各种话题进行讨论，充分表达思想观点和利益诉求。互联网新应用新服务为人们表达意见提供了更广阔的空间。博客、微博客、视频分享、社交网站等新兴网络服务在中国发展迅速，为中国公民通过互联网进行交流提供了更便捷的条件。网民踊跃参与网上信息传播、参与网上内容创造，大大丰富了互联网上信息内容。<br />
　　充分发挥互联网的监督作用。中国政府积极创造条件让人民监督政府，十分重视互联网的监督作用，对人们通过互联网反映的问题，要求各级政府及时调查解决，并向公众反馈处理结果。绝大多数政府网站都公布了电子邮箱、电话号码，以便于公众反映政府工作中存在的问题。近几年，一大批通过互联网反映出来的问题得到了解决。为便于公众举报贪污腐败等问题，中央纪检监察机构和最高人民法院、最高人民检察院等开设了举报网站。中央纪委监察部举报网站、国家预防腐败局网站等开通后，为惩治和预防贪污腐败发挥了重要作用。据抽样调查，超过60%的网民对政府发挥互联网的监督作用予以积极评价，认为这是中国社会民主与进步的体现。<br />
　　高度重视互联网上反映的社情民意。互联网在政府与公众之间架起了直接沟通的桥梁。通过互联网了解民情、汇聚民智，成为中国政府执政为民、改进工作的新渠道，互联网上的公众言论正受到前所未有的关注。中国领导人经常上网了解公众意愿，有时直接在网上与网民交流，讨论国家大事，回答网民的问题。各级政府出台重大政策前，通过互联网征求意见已成为普遍做法。每年全国人民代表大会和中国人民政治协商会议期间，都通过互联网征求公众意见。近三年来，每年通过互联网征求到的建议多达几百万条，为完善政府工作提供了有益参考。<br />
　　互联网为人们享有知情权、参与权、表达权和监督权提供了前所未有的便利条件和直接渠道，为政府了解人民意愿，满足人民需要，维护人民利益发挥了日益重要的作用。中国政府将坚定不移地维护公民依法享有的互联网上言论自由。<br />
