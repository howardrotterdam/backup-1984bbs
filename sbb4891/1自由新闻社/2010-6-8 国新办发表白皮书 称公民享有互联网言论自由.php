																											<div id="postmessage_664253" class="t_msgfont">五、维护互联网安全<br />
　　维护互联网安全是互联网健康发展和有效运用的前提。当前，互联网安全问题日益突出，成为各国普遍关切的问题，中国也面临着严重的网络安全威胁。有效维护互联网安全是中国互联网管理的重要范畴，是保障国家安全、维护社会公共利益的必然要求。中国政府认为，互联网是国家重要基础设施，中华人民共和国境内的互联网属于中国主权管辖范围，中国的互联网主权应受到尊重和维护。中华人民共和国公民及在中华人民共和国境内的外国公民、法人和其他组织在享有使用互联网权利和自由的同时，应当遵守中国法律法规、自觉维护互联网安全。<br />
　　依法维护互联网安全。为维护互联网安全，《中华人民共和国刑法》、《全国人民代表大会常务委员会关于维护互联网安全的决定》、《中华人民共和国治安管理处罚法》、《中华人民共和国电信条例》、《计算机信息系统安全保护条例》、《互联网信息服务管理办法》、《计算机信息网络国际联网安全保护管理办法》等法律法规作出了相关规定，以促进中国互联网的健康发展，维护国家安全和社会公共利益，保护个人、法人和其他组织的合法权益。《中华人民共和国电信条例》第六条规定，“电信网络和信息的安全受法律保护。任何组织或者个人不得利用电信网络从事危害国家安全、社会公共利益或者他人合法权益的活动”。<br />
　　维护互联网信息的安全流动。互联网信息的自由流动与安全流动是相互依存的整体，应在保障互联网信息安全流动的前提下，实现互联网信息的自由流动。中国政府高度重视维护互联网信息的安全流动，积极引导人们依法办网、文明上网、正确用网。《全国人民代表大会常务委员会关于维护互联网安全的决定》、《中华人民共和国电信条例》、《互联网信息服务管理办法》明确规定，任何组织或者个人不得利用互联网等电信网络制作、复制、发布、传播含有下列内容的信息：反对宪法所确定的基本原则的；危害国家安全，泄露国家秘密，颠覆国家政权，破坏国家统一的；损害国家荣誉和利益的；煽动民族仇恨、民族歧视，破坏民族团结的；破坏国家宗教政策，宣扬邪教和封建迷信的；散布谣言，扰乱社会秩序，破坏社会稳定的；散布淫秽、色情、赌博、暴力、凶杀、恐怖或者教唆犯罪的；侮辱或者诽谤他人，侵害他人合法权益的；含有法律、行政法规禁止的其他内容的。上述法律法规是维护中华人民共和国境内互联网信息安全的基本法律依据，所有中国公民和在中华人民共和国境内的外国公民、法人和其他组织都必须遵守。<br />
　　依法打击网络犯罪。近年来，中国的网络犯罪呈上升趋势，各种传统犯罪与网络犯罪结合的趋势日益明显，网络诈骗、网络盗窃等侵害他人财产的犯罪增长迅速，制作传播计算机病毒、入侵和攻击计算机与网络的犯罪日趋增多，利用互联网传播淫秽色情及从事赌博等犯罪活动仍然突出。据统计，1998年公安机关办理各类网络犯罪案件142起，2007年增长到2.9万起，2008年为3.5万起，2009年为4.8万起。为有效打击网络违法犯罪活动，中国法律规定，对利用互联网和针对互联网的犯罪行为，依照《中华人民共和国刑法》的相关规定追究刑事责任；对不构成犯罪的，依照《中华人民共和国治安管理处罚法》、《计算机信息网络国际联网安全保护管理办法》等法律法规予以行政处罚。<br />
　　反对任何形式的网络黑客攻击行为。中国同世界其他国家一样，面临黑客攻击、网络病毒等违法犯罪活动的严重威胁。中国是世界上黑客攻击的主要受害国之一。据不完全统计，2009年中国被境外控制的计算机IP地址达100多万个；被黑客篡改的网站达4.2万个；被“飞客”蠕虫网络病毒感染的计算机每月达1，800万台，约占全球感染主机数量的30%。中国法律禁止任何形式的网络黑客行为。《全国人民代表大会常务委员会关于维护互联网安全的决定》明确规定，对“故意制作、传播计算机病毒等破坏性程序，攻击计算机系统及通信网络，致使计算机系统及通信网络遭受损害”等破坏网络安全的行为，构成犯罪的，依照刑法有关规定追究刑事责任。《中华人民共和国刑法》第二百八十五条、二百八十六条对非法获取计算机信息系统中存储、处理或者传输的数据，对提供专门用于侵入、非法控制计算机信息系统的程序、工具等违法行为的刑事处罚作出了具体规定。<br />
　　各国国情和文化传统不同，对互联网安全的关切也有差异，应充分尊重各国对互联网安全的不同关切，在差异中求和谐，在交流中促发展，共同维护国际互联网安全。<br />
