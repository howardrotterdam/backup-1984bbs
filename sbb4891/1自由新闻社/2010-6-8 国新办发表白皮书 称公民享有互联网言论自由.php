<br />
&nbsp; &nbsp; 白皮书表示，中国政府将不断完善互联网发展与管理政策，使其更加符合互联网发展与管理的内在规律及客观需要。在实践中，中国政府十分注重借鉴各国发展与管理互联网的有益经验，并愿与世界各国一道共同促进世界互联网的繁荣发展。<br />
