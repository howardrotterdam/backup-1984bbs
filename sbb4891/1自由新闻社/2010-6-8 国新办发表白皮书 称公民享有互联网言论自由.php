<br />
&nbsp; &nbsp; 白皮书旨在介绍中国互联网发展的基本情况，说明中国政府关于互联网的基本政策以及对相关问题的基本观点，帮助公众和国际社会全面了解中国互联网发展与管理的真实状况。<br />
