																											<div id="postmessage_52011" class="t_msgfont">《该隐与亚伯》内容丑化中国--国内停播<br />
<br />
该剧因多数外景在中国拍摄，备受中国影迷的期待，但自开播以来，在已经播出的6集中，部分涉及中国的内容与情节不符实际，损害了中国形象，肆意抹黑中国社会。播出后在影迷之间引发广泛争议，斥责编剧剧本内容扭曲失真，伤害到了国人的情感。由首发影视与挚情志燮率先联合发布停止制作的声明，随后凤凰天使等国内主要字幕组也声明停止制作中文版本。<br />
<br />
&nbsp; &nbsp; 本着纯娱乐不政治的观点，本来不想把爱国和爱韩剧分开，但这部剧实在是让我忍无可忍，让我彻底的领教了这位叫朴继玉编辑的无耻和无知，从苏志燮走出浦东机场开始，在机场外与出租车司机砍价，放着上海繁华的街道不拍专拍挂着裤衩，满墙小广告的破旧弄堂和废弃厂房，拍的好像80年代的中国，主角还从韩国带了一大包药品给“贫困的中国百姓”排队义诊（请问他是怎么过安检的？），之后从上海被一帮不会说中国话的伪中国人绑架，用了几小时开车到宁夏银川的沙漠埋掉（脑残的编剧以为中国国土和韩国差不多，几小时之内走完全国），在沙漠又遇见从朝鲜潜入中国的逃兵，准备从中国偷渡到韩国，还在上海街头肆意的枪战，杀人，贩毒，最可气的是剧中的中国监狱竟然设立角斗场（莫非“躲猫猫”这么快就被棒子学会了？）<br />
<br />
我也看了不少韩剧了，去其他国家取景的时候怎么都拍的那么美，怎么到了咱这就成这样了呢，其实这份不满已经积累已久了，《花样男子》的澳门外景，到处是小偷和混混，还有傲慢的在景随意斥责的中国保镖。还有《伊甸之东》 英兰冲着一群要绑架麦克的外国鬼佬大喊：“你们这些中国人要干什么？”所有的坏事都让几个不会说中国话的伪中国人做尽了。<br />
<br />
&nbsp; &nbsp; 扎实的剧本，精美的画面，优秀的演员，唯独不能对邻邦有起码的尊重和真实的反映，影视作品在相当程度上影响着受众的基本认知，当我们如此雍容大度地表现大国风范的时候，韩国媒体却在愈加肆无忌惮地抹黑中国，也在用他们的影视作品影响着中国和韩国那些没有辨别力的青少年。 不想否定韩剧的优秀，只是希望大家不要盲目的追捧韩流。<br />
<br />
&nbsp; &nbsp; 最后想说可惜了这个精彩的剧本了，本想看看兄弟二人是怎样手足相残，重演《圣经》里的一幕的，也可惜了苏志燮精心准备付出后的第一部剧了，怪只怪他选错了剧本。<br />
