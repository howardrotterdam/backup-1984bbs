																											<div id="postmessage_78875" class="t_msgfont">张召忠：中国航母名字应该叫“毛泽东”号<br />
星岛环球网 <a href="http://www.stnn.cc" target="_blank">www.stnn.cc</a> 2009-04-21 <br />
正值中国海军在为建军60周年举行庆典时，外界纷纷在猜测中国是否会建造航母。而在建造航母之后，中国会取什么名字也是媒体关注的焦点。中国著名的军事专家张召忠教授近日在接受媒体的采访时，中国建航母之后，应该效仿美国，按照国家领导人的名字为航母命名，而其中又以“毛泽东”号最好。<br />
　　CCTV《今日关注》报道，有媒体问到，如果要造航母，这个机翼折叠的技术必须解决，像“歼-10”这个技术就必须改造。<br />
<br />
　　张召忠说，平常看电视、看电影都在飞行甲板上一大堆都摆在那里，那是照相用的，真正的要放在机库里面，都放开着翅膀，机库能放几架？必须要折叠，但是机翼折叠不是核心技术，这个可以解决。<br />
<br />
　　张召忠说，航空母舰的防护现在有三个：第一，外形的防护，通过隐身，舰艇外形的设计，“福特”号航空母舰和“尼米兹”相比，外形各方面，比如雷达波射来之后，他可以通过稀薄材料、透玻材料和45度的斜角，通过这些技术，它可以让电磁波不形成反射，一个10万吨的航母在雷达荧光屏上如果原来不隐身，可能反映成一个高尔夫球这么大，但是经过隐身之后，反映可能是一个小米粒这么大。<br />
<br />
　　第二，水深降噪技术，降低你的水深信号，让敌人的潜艇跟踪的时候，可能就会减少被探测的概率。<br />
<br />
　　第三，电磁辐射技术，把你的电磁信号降到最小。<br />
<br />
　　张召忠说，一般要有四到五艘水面舰艇，巡洋舰、驱逐舰、护卫舰护航，另外水下要有一艘到两艘核潜艇为航母护航。<br />
<br />
　　有媒体问道：“不管怎么说，我想中国现在确实是到了应该拥有航母的时候，就像梁光烈部长所说的，中国不能永远没有航母。如果中国可能制造航母，请两位嘉宾给我们中国的航母取的名字，会起什么合适，因为我们现在知道从新闻里熟知的导弹驱逐舰到护航的，“武汉”号、“深圳”号，我们的航母用什么比较好？”<br />
<br />
　　张召忠回答说，最终起名字当然是由海军党委报中央军委批准，我们在这里瞎猜也没有什么科学依据，但是我感觉我们的护卫舰、驱逐舰都有它的规律，比如按照城市有的是按照是地级市，有的是按照大城市，如果征求我个人的意见，中国的航空母舰按照什么样的起，我个人认为应该按照国家领导人的名字，美国都是这样，“里根”号、“华盛顿”号、“艾森豪威尔”号，最新的是“布什”号这个，按照中国领导人，“毛泽东”号。</div>

							
							
							
							
