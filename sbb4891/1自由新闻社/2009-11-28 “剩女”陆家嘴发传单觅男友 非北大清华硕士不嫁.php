<br />
<br />
<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;【推销自己】<br />
&nbsp; &nbsp;&nbsp;&nbsp;自制1300份征婚传单<br />
&nbsp; &nbsp;&nbsp;&nbsp;“今天一共发了500份传单，从早上7点到9点半全部发完。”罗小姐告诉记者，陆家嘴上班的有很多优秀青年，在这里较容易找到心仪男子。<br />
&nbsp; &nbsp;&nbsp;&nbsp;而这已经是她第四次发这样的征婚传单了。从10月23日开始，罗小姐就尝试在地铁一号线和四号线内，向乘坐轨道交通的“目标乘客”挨个派发征婚传单。也曾在漕宝路到淮海中路沿路向行人发过传单，粗略统计下来，四次共发了1300份。<br />
&nbsp; &nbsp;&nbsp;&nbsp;“我不喜欢网络，也很少看报纸，传单是最原始最可靠的方式。”罗小姐这样解释，“发传单可以让人强制阅读，收传单的人总会看一下的。”据她介绍，发传单到现在，已经有七八十名男士给她打过电话。而昨天刚发出去不到3个小时，就接到了18位男士电话。“但是符合条件者寥寥无几，也暂时没有让她心动的。”<br />
<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;【觅偶条件】<br />
&nbsp; &nbsp;&nbsp;&nbsp;非清华北大硕士不嫁<br />
&nbsp; &nbsp;&nbsp;&nbsp;如此众多应征者，为何皆不符合条件？记者在这份征婚传单上看到，罗小姐的择偶条件相当苛刻。“必须为北京大学或清华大学硕士毕业生。”“必须为经济学专业毕业”“必须东部沿海户籍。”“无生育史，年龄在25至28岁之间。”罗小姐开出了七大择偶条件。<br />
&nbsp; &nbsp;&nbsp;&nbsp;“聪明是征婚条件中最重要的一条，我阅人无数，少说两三百人，通过我的比较，北大和清华毕业的人比一般人聪明。”罗小姐称，正因为通过平常的接触，所以才会很肯定地提这样的要求。<br />
&nbsp; &nbsp;&nbsp;&nbsp;“掌握了经济就掌握了世界。”罗小姐认为，经济学是所有学科中最先进的，管理学次之。她希望将来的另一半能是一个精通经济学的人，这样才能一起打拼创造出美好的未来。<br />
&nbsp; &nbsp;&nbsp;&nbsp;而对于觅偶的户籍要求，就连罗小姐的家乡重庆也被拒之门外。罗小姐解释，这是出于种族优化的考虑。“我是重庆人，沿海地区离我比较远，这样两人在一起才能比较稳固，比较有新鲜感。”<br />
<br />
<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;【工作经历】<br />
&nbsp; &nbsp;&nbsp;&nbsp;来沪曾投1万份简历<br />
&nbsp; &nbsp;&nbsp;&nbsp;提出那么多苛刻特别的征婚要求的罗小姐究竟是何许人也？根据她本人的描述，罗小姐生于1985年，重庆人。7岁的时候父母离异，普通师范学校毕业。她的第一份工作是奶粉厂客服，但一个月后她就失去了这份工作，“老板说我心态不好。”<br />
&nbsp; &nbsp;&nbsp;&nbsp;后来，她又去三峡的一所小学做了两年的老师。刚开始，学校让她教六年级语文，但是一学期后由于教得不好，又将她调到了三年级，随后是一年级，最后，她“沦落”为副科老师。据她称，2008年她来到上海，为找工作先后投过一万多份简历，从总经理到服务员，几乎各行各业她都试过。做过连锁餐饮的服务员，也做过电视购物的销售，但是工作时间都不长。现在，罗小姐在从事批发零售行业，月收入不过千余元。<br />
&nbsp; &nbsp;&nbsp;&nbsp;“我是个平凡人，可是我希望我的另一半能聪明过人。”<br />
