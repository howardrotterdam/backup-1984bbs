													<h2>中科院力学所被指遭强拆</h2>
						
						
																											<div id="postmessage_757056" class="t_msgfont">中科院力学所怀柔基地被指强拆 <br />
2010/07/24 05:03 来源：YNET.com 北青网 北京青年报 樊宏伟 <br />
<br />
■挖土机正在拆除该基地实验室房屋 ■供图/中科院力学所 　　“我们以沉痛和愤怒的心情正式宣告，我所怀柔试验基地遭暴力拆毁，钱学森先生回国建立的首批实验室被夷为平地。”中科院力学所昨日在其官方网站发布严正声明称，该试验基地于昨日又遭到二次破坏，直接损失达1700余万元。据记者了解，力学所该试验基地是被中科院研究生院雁栖湖校区施工方拆掉的，该试验基地的拆迁事宜此前已交涉了三年多的时间。 <br />
<br />
　　■试验基地夷为平地 <br />
<br />
　　力学所在声明中指出，“2010年7月17日上午，试验基地的保安人员被一伙不明身份人员控制，失去人身自由。在此期间，共计9处房屋被大型铲车与推土机夷为平地，一批重要的科研装置和设备被砸毁掩埋。”据此前的媒体报道称，工地施工方的20多名保安，在要求力学所留守人员离开后，将实验室、库房和看守人员房间强行砸毁，施工方预付了两天房费，把留守人员安置到了旅馆。 <br />
<br />
　　■称钱学森实验设备被清理 <br />
<br />
　　“2010年7月22日至23日，该试验基地再遭持续地肆意毁坏，钱学森先生回国初期指导研制的科研装备等大量历史性文物、国家973项目试验装备、国防重大科研任务的仪器装置和备件等以“垃圾”的名义被清除出场，值守该试验基地的工作人员深受刺激入院治疗。” <br />
<br />
　　针对声明提到的二次破坏，力学所有关工作人员昨日下午表示，除了一些“拿不走”的大型设备外，试验基地内的设备都被当成是“废弃物”处理了，其中很多是钱学森当年研制的设备，现在虽然“无法用于科研，但都是不可复制的珍贵文物。” <br />
<br />
　　■力学所称损失1700余万元 <br />
<br />
　　声明表示，“该试验基地是钱学森先生回国后亲自选址和创建的，是我国第一个火箭研究与试验基地，为我国‘两弹一星’做出了重大的历史性贡献。目前，该试验基地正承担着国家重大专项、国家重大基础研究发展计划项目等重大科研任务。此次试验基地被毁，初步统计的国有资产直接损失高达1700余万元。一批我国现代科技史上代表性的珍贵文物被肆无忌惮地捣毁和清运，一批国家级的重大科研任务被迫停滞。” <br />
<br />
　　■两次向当地警方报案 <br />
<br />
　　“事件发生后，我们已经在第一时间向中科院有关领导和部门进行了汇报，并向当地公安机关报案。”针对声明中提到的内容，力学所工作人员告诉记者，在事发当天，他们拨打了110报案，并在21日再次进行了刑事报案，明确提出要保护好现场。至于“1700余万元”的损失则是根据试验基地的台账算出的数字。“力学所参与试验基地建设和‘两弹一星’攻关任务的院士和科学家们悲愤交加，全体科研人员极为愤慨。” <br />
<br />
　　■拆迁已洽谈三年多 <br />
<br />
　　据记者了解，力学所试验基地位于中科院研究生院雁栖湖校区的建设工地内，该校区总投资约18亿元，是中科院有史以来投资规模最大的基本建设项目，施工方为北京市城建道桥建设集团有限公司。校区于2006年奠基，2009年10月正式开工建设，预计2011年8月建成投入使用，将满足1万余名研究生的教学、生活和科研需要。 <br />
<br />
　　“这是个遗留问题，之前谈了三年多了，迟迟解决不了。”据有关人士透露，力学所试验基地的拆迁问题已影响到校区的建设进度，中科院的相关领导近期已召开了数次会议协调此事，但在还没谈出结果的情况下就发生了“强拆”。 <br />
<br />
　　对话施工方 <br />
<br />
　　施工方负责人昨接受采访时称―― <br />
<br />
　　拆除本系统废弃房间怎么能叫强拆？ <br />
<br />
　　昨天，针对“中科院力学所怀柔基地遭暴力拆毁”一事，记者采访了中科院建设方和负责施工的相关施工单位负责人，就有关媒体披露的拆除现场细节进行了一一核实。 <br />
<br />
　　■关于“强拆” <br />
<br />
　　“同属中科院怎能叫强拆” <br />
<br />
　　记者：有报道称，中科院力学研究所实验室遭到“强拆”，是否属实？ <br />
<br />
　　回答：首先明确，不属于强拆。中科院研究生院要在其雁栖湖园区建设新校园，这一项目是得到国家及北京市相关职能部门正式批准的建设项目。我们按照国家有关规定，跟研究生院签署了相关施工合同；并按照施工建设的规章制度，我们也确认了此项目的规划证、开工证、施工图等，研究生院还与我们在合同中确定了很严格的施工工期。 <br />
<br />
　　研究生院曾经向我方出示了中科院基建局《关于研究生院报废拆除怀柔新校区废旧房屋的批复》的文件，并委托我们对早已在拆迁范围内的中科院力学研究所遗留实验室剩下的“残垣断壁”实施拆除清运。我们以为，中科院力学研究所也属中科院的下属部门，自己拆除本系统内的废弃房间怎么能叫强拆呢？ <br />
<br />
　　■关于“砸毁设备” <br />
<br />
　　“锈迹斑斑的东西能算科研设备？” <br />
<br />
　　记者：请问是否有“一批重要的科研装置和设备被砸毁掩埋”？ <br />
<br />
　　回答：科学专业我是外行，不懂什么是科研装置和设备。但要说我们看到的那些锈迹斑斑的东西能算是科研设备？打死我也不信。退一万步讲这要真是科研设备的话，也不能就那么扔在那儿，我看少说十几年没人碰过了。还有，我也听说这些平房和院子是“科学试验基地”，可院子里养着几十只鸡、好几条狗，这不，这几条狗还在这儿转悠呢。这院子呀，说是一个破落的农家院还差不多。 <br />
<br />
　　记者：有报道称，共计9处房屋被大型铲车与推土机夷为平地，一批重要的科研装置和设备被砸毁掩埋。 <br />
<br />
　　回答：只有几间平房，屋内的东西大部分早已搬空了，很多房间都没有窗户。中科院研究生院要求我们按图施工，我们只是按图施工。所以我们仅使用几台小型挖土机很快就完成了拆除工作。 <br />
<br />
　　■关于“人员安全” <br />
<br />
　　“我们请出屋内人员有照片为证” <br />
<br />
　　记者：有报道称，现场有退休职工、实验助理6人留守看护。工地施工方突然找来20多名保安，要求留守人员离开。随后，他们将实验室、库房和看守人员房间强行砸毁。其中不少实验设备被毁坏，留守人员的私人物品也被埋在废墟里。 <br />
<br />
　　回答：合同甲方中科院研究生院曾经对我们说过，一定要注意施工安全。当我们发现需拆除的房屋内还有人员时，这些人员又说不清楚他们是哪个单位的，我们以为他们是自己呆在那些废旧房屋内的。因此，我们就把他们请出来了，并请他们带走了他们所有物品。整个过程都有照片为证。 <br />
<br />
　　■关于“是否停建” <br />
<br />
　　“该项目手续齐全估计会按计划进行” <br />
<br />
　　记者：听说该事件已经有警方介入，是否会停止新院的建设？ <br />
<br />
　　回答：在拆除快结束时，确实有警方到了现场，并告诉我们接到110报案。警察到了之后，对现场人员及甲方中科院研究生院进行了有关调查，并对住在平房内的人员进行了询问并作了笔录。实际上，甲方的施工现场很大，涉及好多个建筑物地基，清理渣土等工序并未停止，这些天一直在进行中。由于中科院研究生院新校区建设项目是国家级项目，而且相关手续齐全，依我估计肯定还会按照原定计划进行施工建设。 <br />
