													<h2>中科院力学所基地遭强拆 钱学森研究资料被毁</h2>
						
						
																											<div id="postmessage_757463" class="t_msgfont"><a href="http://news.sina.com.cn/c/2010-07-24/124920749090.shtml" target="_blank">http://news.sina.com.cn/c/2010-07-24/124920749090.shtml</a><br />
<br />
<br />
中广网北京7月24日消息 据中国之声《央广新闻》11时06分报道，上周末，位于北京怀柔区的中科院力学所试验基地大量房屋被突然拆毁，包括钱学森先生当年指导研究的珍贵材料等一批重要科研装置设备被毁，消息一出便吸引了媒体的广泛关注。今天(24日)上午十点，中科院力学所专门就此事召开媒体说明会，详细情况连线在现场采访的中国之声记者王娴。<br />
<br />
　　主持人：这个媒体说明会有没有进行，现在情况怎么样？<br />
<br />
　　记者：原定于今天上午10点钟进行的说明会一直到10点40都没有开始，之后没有以说明会的方式来向媒体说明情况。整个力学所的很多院士、专家，包括力学所相关的领导，以及了解事件情况的人分别向媒体记者说明，大家都处于相对比较散乱的采访状态。<br />
<br />
　　据我了解，拆毁是在17日，也就是上个周末的时候发生的，但是拆毁之后已经跟警方取得联系，警方来介入调查这个事情的原因。据力学所的一位所长告诉我，当时已经拆毁的一些试验设备包括一些还不知道是否废弃的装备，已经通过车辆拉走了。据到过现场的院士和专家告诉我，现在现场已经不是第一现场的状态。<br />
<br />
　　主持人：中科院力学所所在地被强拆的范围大概有多少，损毁程度有多大？另外为什么会突然遭到拆毁？说明会上有没有介绍相关的其他情况？<br />
<br />
　　记者：目前了解到拆毁的大概范围有9栋房屋，还有大量的试验设备，试验设备无法以一个体积或者数量范围来说。这些试验设备有一些是钱学森先生早年回国留下来的历史资料，包括我国重点试验项目973试验项目等等正在进行的爆炸装备和试验设备，都是现在可用并且研究人员正在运用的设备，损失是比较大的。<br />
<br />
　　据到过现场的一个老师告诉我，他在现场碰到了一位自称能够为拆毁现场负责的人，他说他是施工方的人。在拆毁现场旁边就是中科院研究生新园区施工现场的基地，现场的人在那边进行施工。但是为什么突然到这边突然把这些房屋拆毁，力学所方面说始终没有接到过通知，也从来不知道拆毁会发生。但是据拆毁现场的当事人说，他们是为了赶工期、赶进度，所以就把它突然拆毁了。<br />
<br />
　　主持人：谁负责拆工的单位呢？有没有跟他们取得联系，到底是不是由他们下令实施这个拆迁计划？<br />
<br />
　　记者：这因为我所得到的信息还比较有限，但是路桥方面他们已经和当地警方做了笔录取得了联系。我将继续关注事情详细的进展。<br />
<br />
