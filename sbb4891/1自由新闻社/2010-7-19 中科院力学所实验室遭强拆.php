																											<div id="postmessage_759276" class="t_msgfont"><a href="http://www.imech.ac.cn/xwdt/tpxw/201007/t20100723_2909933.html" target="_blank">http://www.imech.ac.cn/xwdt/tpxw/201007/t20100723_2909933.html</a><br />
<br />
关于中科院力学所怀柔试验基地被非法拆毁的严正声明<br />
<br />
我们以沉痛和愤怒的心情正式宣告，我所怀柔试验基地遭暴力拆毁，钱学森先生回国建立的首批实验室被夷为平地。<br />
<br />
2010年7月17日上午，试验基地的保安人员被一伙不明身份人员控制，失去人身自由。在此期间，共计9处房屋被大型铲车与推土机夷为平地，一批重要的科研装置和设备被砸毁掩埋。2010年7月22日至23日，该试验基地再遭持续地肆意毁坏，钱学森先生回国初期指导研制的科研装备等大量历史性文物、国家973项目试验装备、国防重大科研任务的仪器装置和备件等以“垃圾”的名义被清除出场，值守该试验基地的工作人员深受刺激入院治疗。<br />
<br />
该试验基地是钱学森先生回国后亲自选址和创建的，是我国第一个火箭研究与试验基地，为我国“两弹一星”做出了重大的历史性贡献。目前，该试验基地正承担着国家重大专项、国家重大基础研究发展计划项目等重大科研任务。此次试验基地被毁，初步统计的国有资产直接损失高达1700余万元。更加令人痛心的是，一批我国现代科技史上代表性的珍贵文物被肆无忌惮的捣毁和清运，一批国家级的重大科研任务被迫停滞。<br />
<br />
在我国和谐、稳定、快速发展的大好局面下，在中国科学院这一神圣的科学殿堂，发生了如此野蛮的暴力事件，令人震惊！力学所参与试验基地建设和“两弹一星”攻关任务的院士和科学家们悲愤交加，全体科研人员极为愤慨。“炎夏似隆冬，白昼如夤夜”是我们此刻共同的感受。<br />
<br />
事件发生后，我们已经在第一时间向中科院有关领导和部门进行了汇报，并向当地公安机关报案。我们要相信党、相信组织，让我们一起期待法律的公正判决！<br />
<br />
请全体职工和学生克制情绪，保重身体，克服困难，团结起来，勤奋工作，以实实在在的科研工作报效祖国，告慰钱学森先生等的在天之灵！<br />
<br />
