<br />
<a href="http://124.228.254.234/html/art/20100724/122674.html" target="_blank">http://124.228.254.234/html/art/20100724/122674.html</a><br />
京华时报7月24日报道昨天，中国科学院力学研究所官网发表声明称，从7月17日到昨天，力学所怀柔试验基地遭持续暴力拆毁。声明中称，该试验基地是钱学森先生回国后亲自选址和创建的，是我国第一个火箭研究与试验基地，初步统计直接损失高达1700余万元。但是目前无任何一方站出来承认是强拆方。<br />
&nbsp; &nbsp; &gt;&gt;网上声明<br />
<br />
&nbsp; &nbsp; 损失高达1700余万<br />
<br />
&nbsp; &nbsp; 昨天，记者在中科院的官网上看到《关于中科院力学所怀柔试验基地被非法拆毁的严正声明》。声明中称，2010年7月17日上午，试验基地的保安人员被一伙不明身份人员控制，失去人身自由。在此期间，共计9处房屋被大型铲车与推土机夷为平地，一批重要的科研装置和设备被砸毁掩埋。2010年7月22日至23日，该试验基地再遭肆意毁坏。<br />
<br />
&nbsp; &nbsp; 声明中介绍，该试验基地是钱学森先生回国后亲自选址和创建的，是我国第一个火箭研究与试验基地，为我国“两弹一星”做出了重大的历史性贡献。目前，该试验基地正承担着国家重大专项、国家重大基础研究发展计划项目等重大科研任务。此次试验基地被毁，初步统计的国有资产直接损失高达1700余万元。“更加令人痛心的是，一批我国现代科技史上代表性的珍贵文物被肆无忌惮地捣毁和清运，一批国家级的重大科研任务被迫停滞”。<br />
<br />
&nbsp; &nbsp; &gt;&gt;事发现场<br />
<br />
&nbsp; &nbsp; 多辆挖掘机正在施工<br />
<br />
&nbsp; &nbsp; 力学研究所试验基地位于中科院研究生院怀柔新区。昨天下午6点，记者看到，现场四周皆有围挡，上面写着“中国科学院研究生院新园区建设项目”。围挡内工地多辆挖掘机正在施工。<br />
<br />
&nbsp; &nbsp; 工地周围几个大门口都有两名保安把守，保安称力学研究所的确位于研究生院内，目前正在施工，任何人员不得进入。施工工人称对于强拆事件并不知情。<br />
<br />
&nbsp; &nbsp; 力学研究所工作人员称，强拆怀柔试验基地的是北京城建集团下属一单位，他们是中科院研究生院项目施工方。但记者昨天联系城建集团，他们的外宣负责人表示，对此事并不知情。<br />
<br />
&nbsp; &nbsp; 在记者采访过程中，一直有一名白衣男子对记者进行跟踪和拍摄。<br />
<br />
&nbsp; &nbsp; &gt;&gt;留守人员<br />
<br />
&nbsp; &nbsp; 不明身份者强行进入<br />
<br />
&nbsp; &nbsp; 随后，记者在附近村内一家宾馆找到力学研究所试验基地的两名保安。保安李先生称，两个月前，他接受力学研究所安保处处长的委派，赶到力学研究所位于怀柔的试验基地负责安保工作。他到达现场发现，试验基地周围都是工地，基地的7栋平房就像孤岛般立在工地内。“处长要求我们一定要守住试验基地，不让任何人拆”。<br />
<br />
&nbsp; &nbsp; 李先生称，还有几名退休职工和保安留守试验基地。一个月前基地停水，他们每天走到大门口驮水用。7月12日，试验基地停电，“对方想逼走我们”。<br />
<br />
&nbsp; &nbsp; 7月17日上午8点多，李先生回忆，20多名保安涌入了试验基地，“他们一进来，未说明身份就说不准我打电话，不准我动，不准我拿东西”，李先生被带出门外，他看到门口停放五六辆挖掘机，“挖掘机动工拆基地，留守人员的被褥和生活用品均被埋，所有的仪器设备都尚未搬出”。李先生称，试验基地内有很多贵重仪器，其中一台设备是钱学森用过的，看着仪器被毁，他很心疼。<br />
<br />
&nbsp; &nbsp; &gt;&gt;中科院力学所<br />
<br />
&nbsp; &nbsp; 警方介入未能阻止持续强拆<br />
<br />
&nbsp; &nbsp; 17日当天下午，力学研究所的负责人赶到现场，将李先生和另一名保安安排到附近宾馆居住。他们还向警方报了案。3点钟左右，怀柔派出所警察赶到现场，现场停止了强拆。<br />
<br />
&nbsp; &nbsp; “但是让我们没想到的是破坏还在继续”，中科院力学所工作人员介绍，昨天凌晨，他们前去整理损毁物品时发现，此地再次被封闭起来，一些未来得及整理的珍贵设备和资料，包括钱学森先生回国初期指导研制的科研装备等大量历史性文物、国家973项目试验装备、国防重大科研任务的仪器装置和备件等已经作为废弃物进行了清理。“在此地驻守多年的一名老员工，由于受不了基地被毁的刺激已经住院了”，该名工作人员说。<br />
<br />
&nbsp; &nbsp; 附：中国科学院力学研究所声明<br />
<br />
&nbsp; &nbsp; 我们以沉痛和愤怒的心情正式宣告，我所怀柔试验基地遭暴力拆毁，钱学森先生回国建立的首批实验室被夷为平地。<br />
<br />
&nbsp; &nbsp; 2010年7月17日上午，试验基地的保安人员被一伙不明身份人员控制，失去人身自由。在此期间，共计9处房屋被大型铲车与推土机夷为平地，一批重要的科研装置和设备被砸毁掩埋。2010年7月22日至23日，该试验基地再遭持续地肆意毁坏，钱学森先生回国初期指导研制的科研装备等大量历史性文物、国家973项目试验装备、国防重大科研任务的仪器装置和备件等以“垃圾”的名义被清除出场，值守该试验基地的工作人员深受刺激入院治疗。<br />
<br />
&nbsp; &nbsp; 该试验基地是钱学森先生回国后亲自选址和创建的，是我国第一个火箭研究与试验基地，为我国“两弹一星”做出了重大的历史性贡献。目前，该试验基地正承担着国家重大专项、国家重大基础研究发展计划项目等重大科研任务。此次试验基地被毁，初步统计的国有资产直接损失高达1700余万元。更加令人痛心的是，一批我国现代科技史上代表性的珍贵文物被肆无忌惮的捣毁和清运，一批国家级的重大科研任务被迫停滞。<br />
<br />
&nbsp; &nbsp; 在我国和谐、稳定、快速发展的大好局面下，在中国科学院这一神圣的科学殿堂，发生了如此野蛮的暴力事件，令人震惊！力学所参与试验基地建设和“两弹一星”攻关任务的院士和科学家们悲愤交加，全体科研人员极为愤慨。“炎夏似隆冬，白昼如夤夜”是我们此刻共同的感受。<br />
<br />
&nbsp; &nbsp; 事件发生后，我们已经在第一时间向中科院有关领导和部门进行了汇报，并向当地公安机关报案。我们要相信党、相信组织，让我们一起期待法律的公正判决！<br />
<br />
&nbsp; &nbsp; 请全体职工和学生克制情绪，保重身体，克服困难，团结起来，勤奋工作，以实实在在的科研工作报效祖国，告慰钱学森先生等的在天之灵！<br />
<br />
&nbsp; &nbsp; 中国科学院力学研究所<br />
<br />
