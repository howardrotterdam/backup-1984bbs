													<h2>文化部:全国所有文化娱乐场所21日停止一切娱乐活动</h2>
						
						
																											<div id="postmessage_573464" class="t_msgfont">新华网北京４月２０日电（记者周玮）为表达全国各族人民对青海玉树地震遇难同胞的深切哀悼，国务院决定４月２１日举行全国哀悼活动。为贯彻国务院决定精神，文化部２０日下发紧急通知，要求全国所有文化娱乐场所２１日停止一切娱乐活动。<br />
<br />
&nbsp; &nbsp; 通知要求，全国各级文化行政部门、文化市场综合执法机构和各文化单位要按照国务院公告要求，凡悬挂国旗的，下半旗志哀。全国文化系统各部门、各单位停止组织一切娱乐活动。<br />
<br />
&nbsp; &nbsp; 通知要求，全国所有文化娱乐场所停止一切娱乐活动。各影剧院、歌舞娱乐场所、游艺娱乐场所、棋牌室等场所以及文化馆（宫、站）、社区文化活动中心停止一切演出、娱乐、放映、棋牌等相关活动；各互联网上网服务营业场所停止一切游戏、音乐、影视等相关娱乐活动；各互联网文化经营单位停止一切网络音乐、网络游戏、网络动漫、网络影视等娱乐活动。<br />
<br />
&nbsp; &nbsp; 通知要求，各级文化行政部门和文化市场综合执法机构要加强对本地区各类文化娱乐场所及经营单位执行国务院决定的情况进行督查，对违反决定的，依法予以查处。<br />
