　　昨日，记者在广州开发区、萝岗区青年路的西区民营职业中介市场看到，十多家职介在此已经摆开阵势，每家中介前贴满了不同企业的招工广告。不少企业现场即开始面试，招聘台前人头攒动，从全国各地赶来的应聘者排起了长龙。“我们是根据广州火车站的客流情况来确定营业时间的，年初五就开始营业了。”广州开发区、 萝岗区劳动和社会保障局在现场的监管人员表示。<br />
<br />
　　生产手机外壳的贝尔罗斯公司昨日一次拿出了1000多个各个不同部门的岗位，在现场进行面试。来自该公司装饰部的招聘人员俞伟华告诉记者，他们这次的招工标准学历在初中以上，薪酬算上加班工资每月1800元以上。<br />
<br />
　　“今年节后我们提供了1万多个岗位给应聘者，比去年同期多了30%。”民营中介广州市向盈人力资源有限公司劳资主管姚贤营介绍，这1万多个岗位以制造业尤其是电子行业为主。由于感觉到招工存在难度，不少企业都提高了待遇，底薪都超过了860元/月的最低工资标准，普遍达到1000元/月以上。<br />
<br />
　　广州开发区、萝岗区劳动和社会保障局有关负责人表示，从春节过后的劳动力市场情况看，企业如今的态度已经从过去的被动变为主动，由于金融危机过后订单中的长单大单增多，企业可以根据全年的情况来确定人力资源需求计划，春节过后的招聘量明显提升。<br />
<br />
　　难<br />
<br />
　　“招工大户”比比皆是<br />
<br />
　　最缺就是一线普工<br />
<br />
　　“招工非常困难，特别是基层员工。我们在节前就发布了招用人的信息，从年初六就开始全面进入招聘阶段。”康师傅有限公司的人力资源总监周旭告诉记者，在节后，他们普工就需要400~500人，其中最主要的原因是今年新增加了生产线，这一块就需要新增加300人，而其他原因在于一些员工回家之后不回来了。而越是辛苦的岗位越是难招人，该厂的装卸、搬运岗位“根本招不到人，整年都在缺工状态”。<br />
<br />
　　觉得招工难的不仅仅是一个企业，在广州，节后需要招工几百人、上千人的企业比比皆是。新谱电子有限公司每个月都需要新增工人几百人，人事总务长余春霞昨天就已经跑到山东招人；建兴光电有限公司需要招工1000人左右……旺盛的需求主要基于节后生产订单不断，新的生产线增加以及节后农民工回流的减少。“整个市场的需求太大，但是市场的人流、供应相反却比以前减少了。”建兴光电有限公司招聘负责人赵雪辉说。<br />
<br />
　　抢<br />
<br />
　　企业加薪一成留人<br />
<br />
　　劳动部门“出去”招工<br />
<br />
　　招不到人，是不是薪水不够高？涨薪吸引人，几乎成为每个企业招人留人抢人最直接的办法。<br />
<br />
　　“我们在一月份就增加了200多元的补贴。”赵雪辉告诉记者，去年他们的普工人均工资在1500元/月左右，虽然现在底薪没有变化，但加上200多元的补贴，也有10%以上的涨幅。<br />
<br />
　　“每年3月我们都会调整工资。”新谱电子有限公司的余春霞告诉记者，目前一线员工的月工资在1700~2000元左右。<br />
<br />
　　涨薪只是其中一个方面，在另一方面，人力资源主管们纷纷跟内地的劳动力市场或者技校达成协议，直接向工厂输血。<br />
<br />
　　广州开发区、萝岗区劳动和社会保障局副局长李雄慧告诉记者，该区目前约有六七成的企业存在一定的用工缺口，广州开发区、萝岗区将采取“走出去”战略，节后组织企业到甘肃、四川等地举行大型供需见面会，并出台职业技能培训由政府出资“埋单”等优惠政策。<br />
<br />
　　分析<br />
<br />
　　缺工已成全国现象<br />
<br />
　　原因一：长三角也缺工 农民工多转战二线城市<br />
<br />
　　来自湖南的打工妹燕子告诉记者，现在身边的朋友很多都转战内地二线城市了，离家近一点，“比如南昌，月薪并不比广州低，也都有2000元左右了。”招聘主管赵雪辉说，“广州的工资水平不比内地高多少，吸引力不高。”<br />
<br />
　　一方面内地城市发展起来了，缺工已经不仅仅是珠三角、长三角，而是全国性的问题，现在连安徽这样以前的劳务输出地也都发展起来，开始出现缺工现象。<br />
<br />
　　原因二：新生代农民工素质提高倒逼企业升级换代<br />
<br />
　　另一方面，缺工是因为供求的不平衡，企业需要大量的一线工人，但是新成长的劳动力学历越来越高，首要选择已经不再是生产工。新生代劳动力结构的变化，不仅仅影响着企业的招工情况，在一定程度上，也在逼迫着企业升级换代。<br />
<br />
　　长三角链接<br />
<br />
　　杭州 求职的还没有招聘的多<br />
<br />
　　春节刚过，在用工大省浙江省的省会杭州，外来劳动力市场上出现了“招工难”现象，每天来求职的人数还不如招聘的人数多。<br />
<br />
　　年初六就开门的杭州外来劳动力市场，明显没有往年热闹。这两天来，入场求职的农民工稀稀拉拉，每天只有500人左右。而市场内企业提供的岗位数却有将近1万个，招工单位372家，比去年同期增加了71个单位，近4600个岗位。（新华）<br />
<br />
　　义乌 千家企业招工：一切好商量<br />
<br />
　　正月初五，义乌人力资源市场便开门迎接全国各地的建设者。据义乌市就业管理服务局副局长季孝义介绍，目前已有1000多家企业登记招工。<br />
<br />
　　“你先留个电话号码，待遇什么的我们一切好商量”、“只要你来就可以了，完不成任务我们还是给你1500元每月的基本工资”……不少企业除按惯例“包吃住”外，在员工的工资待遇方面也更加优厚，不仅有2000多元的基本工资，还外加午餐补贴、生日祝福金、工龄补贴等，颇为诱人。此外，“带薪培训”也成了新“筹码”。“只要肯来，月薪1500元是少不了的，任务超额完成还有奖金，慢慢熟练了，谁都超额完成，做得好的一个月有4000多元的工资。”一家袜业包装厂负责人说。（中新）<br />
<br />
　　个案<br />
<br />
　　应聘者心态<br />
<br />
　　熟练工：更愿意升级做技工<br />
<br />
　　当记者看到董孝军时，他正目不转睛地盯着大屏幕，用笔做着记录。来自湖南衡阳农村的他今年30岁，但在广州已经度过了10年的打工生涯。从初七开始，他每天都要来这里转一转。“去年底刚刚考取了叉车的资格证，今年想换一份更有技术含量的工作。”憨厚的董孝军向记者谈起了自己的新年计划。<br />
<br />
　　董孝军告诉记者，去年他还在太和镇龙归的一家金属制品厂做普工，每天干十多个小时，收入才1400多元。“工作比较累，挣得也不多。”董孝军后来一咬牙，拿出近1个月的工资参加叉车资格培训，两个月后他顺利拿到了叉车资格证。<br />
<br />
　　新生代：对收入期望值更高<br />
<br />
　　在新市墟一家职业介绍中心内，两个湖南小姑娘引起了记者的注意，她们的年纪都还很小，“初中毕业后就跟着老乡出来了。”<br />
<br />
　　记者随机采访了几名正在找工作的年轻人，很多人都表示首选是进电子厂做普工。“因为听说那里的工作基本都是坐在凳子上干的，不会太累，而且是计件工资，干得多拿得多。”记者走访了多个招聘市场也发现，对于物流企业、餐饮企业和化工行(601398,股吧)业提供的相关普工岗位，很多新生代农民工并不感兴趣。虽然没有工作经验，但是小姑娘对工资的要求可不低：“曾有一家制品厂招聘人员告诉我，计件收入每个月都有2000元，工作又不难。”<br />
<br />
　　缺人行业排名前三<br />
<br />
　　电器制造业<br />
<br />
　　机械加工业<br />
<br />
　　服装制鞋业<br />
<br />
　　广州市人力资源市场服务中心主任张宝颖接受记者采访时称，广州最缺工人的首先是加工制造业，多集中在电器制造业、机械加工业、服装制鞋业等。这一行业的用工需求占了广州用工总量的45%左右。此外还有服务业，主要集中在物流配送业、会展商贸等。<br />
<br />
　　另据新华社报道，张宝颖表示，珠三角的“用工荒”主要属于结构性缺工，企业招工呈现“两头重、中间轻”的“哑铃形”，即对技工、普工的需求量较大，而对处于中间的管理类、文员类等需求量不大。这种结构性缺工也影响到缺工的区域性分布，以广州为例，加工制造业比较集中的番禺区、白云区、增城市缺工较多，而商贸比较集中的市中心就不突出。<br />
<br />
　　珠三角<br />
<br />
　　广州节后缺工至少20万<br />
<br />
　　“广州今年用工至少要补充20多万。”张宝颖昨日表示，节后的用工短缺在节前已现出端倪。<br />
<br />
　　去年全年统计显示，全年进入人力资源市场求职的劳动力有150万左右，而用工需求数量则达160万，出现10万用工缺口。根据他们节前的一项统计显示，广州登记备案的225万多外来工中，去年春节有70%回乡过年，大约是160万，估计返岗的比例在90%~95%之间，中间就缺了十几万，外加今年用工需求增加，广州今年用工至少要补充20万。<br />
<br />
　　东莞<br />
<br />
　　普工需求高达八成<br />
<br />
　　东莞市劳动局的统计数据显示，目前岗位需求量约35万个，其中流动变动约15万个，缺口约20万个，岗位需求中普工需求占80％。（新华）<br />
<br />
　　深圳<br />
<br />
　　一个月近六百场招聘会<br />
<br />
　　深圳21日正式启动“‘春风行动’免费专场招聘会”，将持续至4月30日，活动期间将举行免费专场招聘会573场，比去年增加一倍。（新华）<br />
【作者：蒋悦飞 赖伟行 黄琮 来源：大洋网-广州日报】 (责任编辑：陈正红) <br />
