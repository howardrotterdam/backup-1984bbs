																											<div id="postmessage_487214" class="t_msgfont">沿海地区出现严重缺工现象 珠三角缺工超200万<br />
<br />
2010年02月21日08:34<br />
<br />
　　深圳:大量需要制造业工人<br />
<br />
　　随着经济的企稳回升,深圳的用工需求回到金融危机前的水平。其中深圳的制造业需要大量普工,月工资基本在1700―1800元(含加班费),且企业都放宽了用人标准,45岁以内的农民工也能在深圳找到工作。<br />
<br />
　　昨日,记者电话连线深圳三和职介总经理许琴女士。她介绍,深圳制造业,主要是电子电器、玩具、制鞋、五金等劳动密集型行业,用工缺口很大。为招到人,现在深圳有些企业变相地、悄悄地增加了工资,普工们每月在加班后的薪水可达1700― 1800元,如果加班量再大些,薪水可以达到2000元,但劳动强度也很大。普工招工标准,许琴介绍说,也放宽了许多,以前都要招25岁以下,初中以上文化的女性。现在,制造业基本上对学历没有要求,性别没有了限制,年龄放宽到了45岁以内。<br />
<br />
　　深圳酝酿上调最低工资标准<br />
<br />
　　深圳市《2009年第四季度劳动力市场供求状况分析》显示,去年第四季度该市劳动力需求人数达194.4万,求职人数为112.5 万,用工缺口为81.9万。<br />
<br />
　　昨日,针对日益严重的用工荒情况,深圳市人力资源和社会保障局有关人士明确表示,今年深圳将上调最低工资标准。深圳市上次调整最低工资标准是在 2008年7月,标准为特区内1000元/月,特区外900元/月。有调查数据显示,70%的人认为深圳市的最低工资标准应该定在1500元到2000元之间。<br />
<br />
　　今年春节前,广东东莞、中山等市,上调最低工资标准留人的呼声也甚高。据当地媒体报道,1月末的东莞“两会”上,市政协委员、长安添诚儿童用品有限公司董事长杨福寿大胆进言,东莞最低工资标准应由现在770元调至900-1000元。理由是:一味维持最低工资不变,让外来人员逐步流向其他城市。届时企业有订单,但招不到人、交不了货、不得不多付3倍的工资请工来做。<br />
<br />
　　广州:节后三大行业缺工15万<br />
<br />
　　据广州媒体报道,广州加工制造业、现代服务业、传统服务业等三大行业,缺工最为严重,缺工量预计达15万!这一消息,来自广州市人力资源市场服务中心主任张宝颖。<br />
<br />
　　据张宝颖分析,今年的缺工与前几年所谓“民工荒”时的缺工相比,已发生了较大的变化。当时反映缺工的多数是一些制衣厂、电子厂等简单加工型企业,而且企业一招就要数百上千人。今年节后缺工,比较集中在三大行业:加工制造业、现代服务业和传统服务业。特别是餐饮业的前台和楼面服务员,平均1名求职者有近5个对应岗位可选择。<br />
<br />
　　东莞:“世界工厂”缺工过百万<br />
<br />
　　昨日,东莞市最大的人才市场――智通人才举行节后首场招聘会,200余家企业进场招聘,提供岗位4000多个,吸引了2000余人进场求职。这些岗位大多是技术和管理性岗位,月薪普遍在1800元左右。<br />
<br />
　　昨日,智通人才招聘部蔡晓梅经理接受记者电话采访时称,由于缺工严重,今年东莞的企业招工都比去年“起”得早。去年受金融危机影响,很多企业春节后推迟开工,该市场首场招聘会,也不得不推迟到正月初十举行。今年,东莞不少企业正月初六就早始“抢人”。<br />
<br />
　　据东莞媒体报道,从去年8月起,有“世界工厂”之称的东莞,用工短缺开始显现,随后愈演愈烈。经济回暖速度之快出乎人们意料,一些企业,几乎是前脚将工人辞退,后脚即开始招工。<br />
<br />
　　湖北驻东莞劳务管理办事处负责人朱智秀,在接受记者电话采访时介绍,据他们从各种渠道了解到的信息,东莞目前缺工近30%,已接近该市金融危机发生之前的状态。据此推算,拥有500多万外来打工者的东莞,缺工当在百万以上!<br />
<br />
　　节后,东莞缺工最严重的是电子、家具、制衣、玩具等劳动密集型企业生产线上的普工。<br />
<br />
　　记者去年采访过的一名在东莞大朗镇经商多年的湖北人介绍,以毛织、家具制造业闻名的大朗镇,眼下多数企业缺工达20%-30%,严重的缺工达 50%。一家拥有近3000名员工的毛织厂,由于节后订单大增,缺工达1300余人,已严重影响到该厂的生产。<br />
<br />
　　此外,由于进行产业结构调整,东莞不少企业正由劳动密集型向技术型转型,使得技术工人的需求猛增。据官方消息,目前东莞数控编程、工模具师傅、电工、焊工等技术工人,缺口不下 3万人。<br />
<br />
　　由于缺工严重,东莞许多企业意识到,留住老员工与招聘新员工同等重要。在春节前,一些企业为留住老员工,频出“奇招”。<br />
<br />
　　记者了解到,为防止员工离职,东莞企业将一个月工资拖后一个月发放已成惯例。但节前,一些企业打破惯例,将两个月工资拖延到春节后发放。昨日, 在东莞某家具厂打工的湖北公安籍农民工彭远喜告诉记者,他所在的家具厂由于招不到新人,现有员工只好一人顶俩,每天工作时间长达11小时。由于工厂将两个月工资拖到节后发,过完春节他不得不再到工厂报到,要不然就“亏大了”。<br />
<br />
　　还有一些企业,通过上涨底薪,给带来新人的老员工50-100元奖励,给新员工报销路费等方式,留人和招人。<br />
