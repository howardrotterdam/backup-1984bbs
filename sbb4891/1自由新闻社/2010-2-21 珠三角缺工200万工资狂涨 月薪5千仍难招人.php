<br />
　　“目前工人实在不好招,要是有人请及时联系我们”!昨日,记者通过上海招聘网站随机采访多家上海企业,得到的答案却异口同声。<br />
<br />
　　记者在杨浦区上海华铭劳务公司发布的招聘信息中看到,其欲招聘30名叉车工,工资2200元至2800元。记者联系该公司招聘负责人徐小姐,徐小姐表示,去年年初叉车工工资加奖金只有2000元左右。但去年下半年开始,用工日趋紧张,现在虽然将工资加奖金提到2800元,可招聘情况仍不乐观。记者问及缘由,陈小姐颇为无奈地说:“现在工人实在不好招。”<br />
<br />
　　记者又找到位于黄浦区的一家企业,这家企业要招机械工,工资1500元外加奖金。记者拨通该公司联系人王女士的手机,她也连称不好招工。并一再询问记者:“你们那里有没有人来？如果有,请随时和我联系!”<br />
<br />
　　上海闵行区外地来沪人员就业服务中心负责人介绍,目前仅闵行区用工缺口就有1000多个,主要集中在制造业、服务业等劳动密集行业。目前企业纷纷将工资上涨 10%-20%,却仍然吸引不到工人。虽说工厂还不至于停工,但影响到企业生产效率已是不争的事实。“但愿正月十五之后,用工荒会有所缓解。”她说。<br />
<br />
　　上海市农民工联席会议办公室主任赵建德接受记者采访时表示,上海用工短缺势头去年4季度就已出现,但就目前来看尚在正常范围,严格意义上还不能称之为“用工荒”。目前出现用工短缺,主要是因为去年企业在经济危机中订单减少,导致工厂减员。而4季度经济形势回暖,企业订单多了起来。可一时难以找到这么多工人,以致生产受到影响。特别是一些劳动密集型企业,流水线甚至开不了工。现在,不仅用工成本增加,工人对工资的期望值也越来越高。目前上海普通工人月薪 1500元左右,很多工厂虽有不同涨幅,也一时难以招到工人。<br />
<br />
