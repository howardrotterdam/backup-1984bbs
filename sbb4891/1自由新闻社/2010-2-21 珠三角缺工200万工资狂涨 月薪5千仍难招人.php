																											<div id="postmessage_489480" class="t_msgfont">我在珠三角长三角工作，和各种各样大大小小的供应商打交道将近16年。有几点看法：<br />
<br />
1，长三角其实也面临严重的缺工问题，其严重性和珠三角并没有什么本质的区别；<br />
2，中国现在大多数的加工型产业，毛利水平在8-12%左右，如果工资水平增加而卖价不增加的话，就像上面的一位同学说的，倒闭是必然的；<br />
3，为什么不增加卖价？因为这些加工型的工厂，并没有特别的竞争优势，下单者掌握着产品的设计，品牌，还有渠道，客人，所以他们并没有讨价还价的能力，加价往往代表着订单的丢失，更何况现在物价通胀，原材料价格将近前两年低位时候的两倍；<br />
4，老板是不是暴利？也许有买手牟利而产生暴利的，但市场不是傻瓜，你一做加工的企业，暴利何来？<br />
5，国家提高普工的最低工资水平，是不是能够通过政府的强制行为来使企业集体涨价，强迫客人接受集体提价呢？会有所帮助，但是帮助不会太大。其一，客人可以将单转给其他国家，象越南，柬埔寨等等，现在中国的优势是某些产业的供应链比较完整，但是一旦用工成本高于转移供应链的成本，订单自然就会转给低成本的国家。其二，国家的监管力度非常低，往往造成遵纪守法的公司成本上涨倒掉。<br />
<br />
所以这里头的问题我觉得有几点：<br />
1，产业的落后问题，这个恐怕会影响中国许多年，因为中国的产业创新能力我觉得基本是零。很简单的问题（虽然很不想看到）：落后的产业，凭什么付多些给普工？会有谁来帮你买单？<br />
2，新生代的工作生活态度问题。楼上有人对我说的不满，这个见仁见智，不多说了。<br />
3，物价成本上涨的问题，如果在异地生活成本上涨，比所你能够得到的还要高，那为什么还要外出务工呢？<br />
4，当然正面的，也有内地发展的原因，不过实际上仅仅只是把目前沿海的问题象内地延伸，沿海面临的问题估计几年后内地同样需要面对了。</div>

							
							
							
							
