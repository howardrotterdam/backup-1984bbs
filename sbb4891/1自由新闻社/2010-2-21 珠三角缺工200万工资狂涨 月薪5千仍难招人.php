<img src="http://img.hexun.com/2010-02-22/122720309.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　节后一周，内地沿海及内陆地区同时出现严重缺工现象。（图片来源：香港文汇报）<br />
<br />
　　中新网2月22日电 据香港《文汇报》报道，节后一周，内地沿海及内陆地区同时出现严重缺工现象，当中尤以闽粤等东南沿海地区为甚。有业界人士估算，整个珠三角地区的劳工缺口料达二三百万，90%企业在年后有招工需求。<br />
<br />
　　目前，广东各地的劳工薪水都已不断水涨船高，其中普工均升至1700元以上，而中山等地的技工月薪更达到3000至5000元，但即便如此，用工缺口仍然难以填满。<br />
<br />
　　据内地媒体报道，随着经济回暖，珠三角地区各城市缺工超过200万人，部分生产线停工，企业纷纷上调工资，降低学历、年龄等限制，希望用工荒得以缓解。比如深圳的用工需求就已回到金融危机前的水准，其制造业目前需要大量普工，月工资基本在人民币1700到1800元左右，且企业都放宽了用人标准，45岁以内的农民工也能在深圳找到工作。<br />
<br />
　　而中山市缺工约13万人，其中普工占70%，技工占30%，最缺工种集中在家电安装、机械冲压等普工，其月薪1500到2500元；电工、焊工、钳工等技工，月薪多在3000元以上，最高可拿到5000元。<br />
<br />
　　报道还称，东莞地区缺工百万。对此，东莞人力资源界人士认为，该说法难以判断，因为目前劳工还在回流中。不过，有东莞东火车站人士表示，尽管最终客流量需等到元宵节后才能知晓，但从目前来看，今年回流劳工比往年要少。全国最大连锁人才市场智通人才市场的蔡小梅经理认为，珠三角毕竟有这么多城市，累积缺口可能超过二三百万。<br />
<br />
　　有业者表示，目前赴沿海打工的农民工已有不少是“第二代”，这些80后和90后的新一代农民工，其教育水平普遍增高，对薪资的要求也随之增长，导致企业的加薪幅度还是跟不上他们的薪水期盼度。<br />
<br />
