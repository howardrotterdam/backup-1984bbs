<br />
　　“只要愿意到中山来,就不愁找不到工作。”张友胜肯定地说。<br />
<br />
　　广西北部湾:3年内可供岗50万<br />
<br />
　　昨日,记者从广西自治区人力资源和社会保障厅获悉,按照广西北部湾经济区现在的发展速度,未来2~3年内,可提供40~50万个工作岗位。到 2015年,北部湾经济区人才需求量将达100万人。<br />
<br />
　　据悉,北部湾经济区主要是南宁、北海、钦州、防城港、玉林、崇左六市,该厅透露,在金融危机影响过后,北部湾经济区拉动就业明显,特别是制造业、电力、煤气及水的生产和供应业、建筑业等行业,用工需求量大增。<br />
<br />
　　柳州市就业服务中心最近公布的一份用工需求显示,推销展销人员、房地产业务员、机械冷加工工、油漆工、运输车辆装配工、冷作钣金加工工等需求大于求职,其中推销展销人员用工缺口达4000个以上。<br />
<br />
