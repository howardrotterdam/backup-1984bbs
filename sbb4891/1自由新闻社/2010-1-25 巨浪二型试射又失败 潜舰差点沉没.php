													<h2>港台媒体：中共秘射导弹坠海砸坏潜艇</h2>
						
						
																											<div id="postmessage_452336" class="t_msgfont">1月25日，一则关于中共军方在黄海秘密试射导弹失败、并砸坏发射导弹的潜艇的消息在港台媒体中广泛刊载。该消息首先由《自由时报》登出，被多家其它媒体转载。不过中共当局及军方截止目前保持沉默，未证实相关消息。 <br />
台湾《自由时报》周一(1月25日)引述资深官员称，中共军队数月前曾在黄海，以经改装的“高尔夫”级弹道导弹潜舰作为导弹试射平台，对“巨浪2”型导弹进行未公开的水下试射。 <br />
不过，当导弹冲出水面后，无法顺利点火，导致导弹又坠回海中，击中“高尔夫”级潜舰本身。 <br />
官员称，由于弹体重达十余吨，造成潜舰严重损坏，差点沉没，经抢救勉强驶回母港进厂大修。 <br />
该官员解释，水下发射导弹难度很高，受限于水下无法点燃引擎，导弹必须靠超高压气体推动，才能从舰身弹射出去直到飞出水面，当弹尾喷射器离水后方可点火升空。但导弹从潜艇发射，直到飞出水面期间，角度必须保持在特定范围内，点火后才不会乱飞。 <br />
据悉，中共军方多年前已在092夏级核动力弹道导弹潜舰上，配备“巨浪1”型潜射导弹，虽然配备核弹头，但因射程太短，仅2000公里左右，加上此舰只在中国沿海活动，不具备实际吓阻效果。 <br />
为此，中共军方十余年前便开始研发射程达8000公里的“巨浪2”型导弹，为“东风31”型导弹的潜射版。 <br />
报导称，据美日的侦测，中共军方数年前曾让“高尔夫”潜舰浮上水面，试射过一枚“巨浪2”型，导弹落在塔克拉玛干沙漠。 <br />
水面试射虽然获得成功，但4次水下试射却屡屡受挫，至今尚无成功试射记录，显示技术面临瓶颈。</div>

							
							
							
							
