													<h2>德总理默克尔在美国会发表历史性演说</h2>
						
						
																											<div id="postmessage_312745" class="t_msgfont"><img src="http://i33.tinypic.com/mjy6u1.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
GETTY IMAGES<br />
WASHINGTON - NOVEMBER 03: German Chancellor Angela Merkel addresses a joint session of the U.S. Congress on Capitol Hill November 3, 2009 in Washington, DC. Merkel became the first German Chancellor to address the U.S. Congress.<br />
<br />
<strong>“柏林的自由之钟，正如费城的自由钟，是提醒我们自由不是从天上掉下来的标志，而必须透过奋斗而来，并在我们的生命中日日维护。”</strong><br />
<br />
【大纪元11月4日讯】(大纪元记者亦平美国华盛顿DC报导) 11月3日，到访美国的德国总理默克尔与美国总统奥巴马在白宫举行了会晤，随后默克尔向美国参、众两院联席会议发表演说。这是过去50年来德国总理首次在美国国会演讲，被视为历史性演说。<br />
<br />
上午10点40分，当默克尔出现在美国国会大厦众议院会议大厅时，坐在听众席上的数百位美国参、众议员起立长时间热烈鼓掌。默克尔说，这一场面令她很感动。<br />
<br />
柏林墙倒塌前 不曾奢想前往美国<br />
<br />
在三十多分钟的演说中，55岁的默克尔回顾了她早年在东德的生活经历，以及她对西方自由的渴望。默克尔说，二十年前，也就是在柏林墙倒塌之前，她甚至连到美国旅行都不曾奢想，更不要说能站在美国国会发表演说。<br />
<br />
她说：“对我来说，很长时间都不可能到达这片充满无限机会的土地。那座墙、铁丝网以及向那些试图逃离的人们开枪的命令限制我接近自由的世界。<br />
<br />
默克尔说，像许多十几岁的孩子一样，她也喜欢牛仔裤，她的一个姨妈经常寄给她在东德买不到的一种牌子的牛仔裤。她说，当时只能藉助于电影和书籍了解美国，其中很多都是她的亲戚从西德偷偷带回东德的。<br />
<br />
她说：“我对美国梦充满激情，每一个人都可能成功，通过个人努力在生活中使梦想成真。”<br />
<br />
感谢美国帮助德国实现统一<br />
<br />
默克尔在演说中感谢美国帮助德国实现统一。她说，德国人民永远都不会忘记在1989年11月9日柏林墙倒塌前以及之后的这些年当中，美国人民所给予他们的支持。默克尔说：“我们知道我们欠你们很多，我永远都不会忘记这一切。”<br />
<br />
在充满情感的演说中，默克尔感谢肯尼迪、里根、老布什等几位美国总统在冷战期间与西德站在一起，共同抵抗共产主义。<br />
<br />
当默克尔在演说中提及美国前总统里根1987年在柏林勃兰登堡门前发表著名演说时说“戈尔巴乔夫先生，打开这扇门吧！戈尔巴乔夫先生，推倒这堵墙吧！”，全场听众起立，掌声经久不息。<br />
<br />
默克尔说，里根的这一呼吁将永远留存在她的心中。<br />
<br />
默克尔说，她曾在东柏林科学院从事科研工作，在柏林墙倒塌之后她决定进入政坛。柏林墙倒塌后的第二年---1990年，默克尔和她的先生第一次飞往“蕴育自由和独立精神”的美国，抵达加州。<br />
<br />
