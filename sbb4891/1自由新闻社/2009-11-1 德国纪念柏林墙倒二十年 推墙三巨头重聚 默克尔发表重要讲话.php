													<h2>言“东德佬”自罚一万欧元</h2>
						
						
																											<div id="postmessage_309406" class="t_msgfont"><a href="http://sports.cn.yahoo.com/051026/324/27qq7.html" target="_blank">http://sports.cn.yahoo.com/051026/324/27qq7.html</a><br />
<br />
雅虎体育讯 ２００５年北京时间10月26日凌晨，德国杯第二轮进行了8场较量。虽然大部分比赛都是在实力相差悬殊的两方之间进行的，看起来几乎毫无悬念，但结果却使球迷和媒体大跌眼镜，比如目前德甲联赛成绩出色的沙尔克04以0：6惨败给法兰克福。而凯泽斯劳滕客场4-2击败了来自前东德的德乙球队埃尔福特虽然算不得什么新闻，但比赛中凯泽主教练亨克因认为己方球员受到不公平对待而在场边破口大骂，将埃尔福特队队员蔑称为“他妈的东德佬”。 <br />
<br />
据埃尔福特队员布鲁纳曼事后描述，比赛中凯泽队员法比安受伤倒地，而埃方队员没有注意到这一事实，还在继续比赛，然后亨克便认定埃尔福特队员缺乏公平体育精神，故意不将球及时踢出边线。“他骂我们是流氓和东德佬。”布鲁纳曼在复述当时场景时仍然掩盖不住自己的愤怒。 <br />
亨克一时冲昏了头脑以至于口不择言，劳滕主席延基却意识到了问题的严重性。他在比赛后马上向埃尔福特全队道歉，并表示要捐献一万欧元以用于埃尔福特的青年队建设。当然，这一万欧元是由主教练亨克来买单的。“既然他这样说了，他就必须为自己的错误付出代价，这是俱乐部的原则。我们希望能取得埃尔福特俱乐部的谅解，无论通过何种途径。”延基在接受采访时严肃地表示。 <br />
但这事还不算完。德国足协也表示，决不能容忍和姑息这种行为。足协正在讨论这个问题，具体的处罚方案大约在周末就会出台。 <br />
但埃尔福特主教练道奇夫认为无论怎样的重罚都不为过：“这是性质非常恶劣的行为，不是单纯道歉就能解决的问题。这不仅是对我们球队，而是对所有前东德人民的侮辱。”不管怎么说，从目前形势看来，亨克如果要彻底弥补因自己的冲动和粗鲁所犯下的过错，一万欧元恐怕是远远不够的。（Susiguo） <br />
