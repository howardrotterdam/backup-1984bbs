																											<div id="postmessage_308749" class="t_msgfont">美德苏联前巨头重聚话当年<br />
<br />
（法新社柏林31日电） 德国前总理柯尔（HelmutKohl）、美国前总统老布希（George Bush）和苏联最后一位总统戈巴契夫（Mikhail Gorbachev）今天重返柏林，推崇彼此紧密的友谊；20年前，这三位冷战时代末期的领袖共同见证柏林围墙倒塌。<br />
<br />
柯尔（执政期间1982年到1998年）、老布希（1989年到1993年）与戈巴契夫（1985年到1991年）三人，出席纪念柏林围墙倒塌20年的活动，坐在典礼台上共同回顾1989年的情景。 <br />
<br />
柯尔说：「戈巴契夫与老布希当年是德国相当重要的伙伴。」柯尔现年79岁，2008年2月髋部骨折后，仰赖轮椅行动。 <br />
<br />
他表示：「我与两位先生的关系之深无与伦比。」 <br />
<br />
人称「统一总理」的柯尔，曾中风造成脸部下半肌肉瘫痪，讲话有些障碍。他称赞老布希，表示如果心情不好，只要打电话给老布希，就会转好。这番话引发现场1800位来宾大笑。 <br />
<br />
今天出席的贵宾除了各国使节以外，还包括新当选连任的德国总理梅克尔（Angela Merkel）。 <br />
<br />
德国11月9日将举行大型庆祝活动，纪念柏林围墙倒塌20年，这导致苏联共产主义垮台与东西德统一。 <br />
<br />
老布希现年85岁，2007年髋部动过手术，如今行动不便。他今天以「前同事」称呼柯尔和戈巴契夫，也说柯尔「确实是20世纪伟大的政治家」。 <br />
<br />
78岁的戈巴契夫提及英国前首相佘契尔（MargaretThatcher）夫人与法国已故总统密特朗（FrancoisMitterrand），英法两国当年与美国一同占领西德。这两人今天都不在现场，佘契尔现年84岁，罹患老人失智症；密特朗已于1996年过世。 <br />
<br />
戈巴契夫说：「佘契尔、密特朗和我自己，我们当年都曾认为必须维持两个德国。」 <br />
<br />
他对柯尔说：「抱歉，我们当年一开始的关系还没那么好。」 <br />
<br />
9日将举办自由节（Festival of Freedom），梅克尔将接待戈巴契夫、波兰反共领袖华勒沙（LechWalesa）和英国首相布朗（Gordon Brown）等人。 <br />
<br />
法国总统沙柯吉（Nicolas Sarkozy）与俄罗斯总统麦维德夫（Dmitry Medvedev）也将出席，美国总统欧巴马将由国务卿希拉蕊&#12539;柯林顿（Hillary Clinton）代表参加。（译者：中央社蒋天清）</div>

							
							
							
							
