[节译]齐泽克：柏林围之倒塌二十年<br />
L三, 2009-11-11 02:36 ― 阿野<br />
（原刊十一月九日the new york times。经译者节录及加上小标题）<br />
<br />
今天是柏林围倒下的二十周年纪念。在这反思的时节，许多人都强调当年事件近乎奇的性质：梦想终于实现了，一众共产政权像骨牌般倒得一塌煳涂，早几个月还完全不能想像的情况，旦夕间便发生。在波兰，谁又能想像会有自由选举，谁又能想像莱赫&#8231;华勒沙（前团结工会的领袖）能真的当上总统？<br />
<br />
可惜，接手的资本主义民主现实，却将丝绒革命朦胧的神圣无情地吹散。人民以不同方式发其理直气壮的失望：有些人表达他们对共产主义年代的美好日子的乡愁；右派则发动民族主义的民粹运动；更新近的，则表达为迟来的反共妄想症。<br />
<br />
比较有趣的是近年于匈牙利及斯洛文尼亚等国家复辟的反共浪潮。二零零六年秋天，反对执政社会党的大型示威把匈牙利折腾了好几星期。反对者把经济危机，归结为因为执政的是共产党的承继人。虽然国家的领袖乃是民主选举产生，但示威者否认其统治的合法性。尤有甚者，当警察出动回复市面秩序，有论者则把情况比喻为一九五六年的苏联军队镇压反共叛乱。这浪反共的恐慌，甚至慢延到符号领域。二零零八年六月，立陶宛立法禁止公开展示m子和镰刀等共产主义像徵，连苏维埃国歌也在禁止之列。二零零九年四月，波兰政府建议将对极权主义宣传的禁制，延伸到共产主义的书刊，衣服等其他物品：不要穿哲古华拉的tee，随时拉得。<br />
<br />
在斯洛文尼亚，难怪民粹的右派对左派最大的攻击，就是旧共产政权的「残馀势力」。如此令人窒息的气氛e，任何新形势和新挑战都被化约为旧斗争的重}，有些极端的说法（不止斯洛文尼亚，有时波兰都一样）甚至认为，同志平权运动及堕胎合法化运动，都不是过是邪恶的共产主义意图令国家道德沦丧的阴谋。<br />
<br />
误认再误认<br />
<br />
反共力量的复辟，究竟是哪e来的力量？在这些国家，不少年轻人对共产党统治甚至连基本的记忆都从缺，这批反共「老鬼」何以突然咸鱼翻生？这波新兴反共力量的回答，一句到尾：如果资本主义真的远胜社会主义，为甚麽我们今天仍然活得如此可怜？这话的意思是，许多人根本不相信他们已活在资本主义之下，认为他们并未享受到真正的民主，他们有的不过是骗人的面具。变天前的邪恶力量仍然把扯线公仔的线牢牢地拿着，一小撮前共产党员伪装成新的资本家及管理层。换言之，铁幕倒下是假的，所以需要另一次起义，革命必需重}。<br />
<br />
这批迟来的反共分子未能洞悉的，其实是他们对当下社会的印象，与哪怕是最传统的左派对资本主义社会的印象，竟然不谋而合：形式民主遮蔽了政治由少数富人操纵的事实。换句话说，新生反共分子无法明白，被他们批评为倒错的、伪资本主义的，恰恰就是资本主义本身。也有批评指，当旧共产政权倒台，相比起民众运动的异见份子，幻灭了的前干部往往更适合成为新资本主义的旗手。当新的反共英雄还在梦想一个更公义、诚实和团结的新社会时，前共产党干部已是不惜一切使出他们新旧的下三滥手段，务求令自己快投入资本主义的游戏规划，与及市场效率冰冷无情的世界。<br />
<br />
更刁的难道不是以下这些共产主义国家麽：一方面让资本主义尽情绽放，另一方面却紧紧抓着政治权力。论这种国家的资本主义的程度，恐怕连西方自由主义资本主义国家也犹恐不及。在这种疯狂的双重颠倒中，资本主义表面上是压倒了共产主义，但这场胜仗的代价，却是共产主义者在自己的大厅e，把资本主义狠狠击败。这就是中国今天为何如此令人不安的原因：资本主义与资本主义，向来都被认为是双胞胎。到今天眼见资本主义在中华人民共和国遍地开花，许多论者还相信政治民主最终会降临神州大地。<br />
<br />
若这中国版本的威权资本主义，最终证实是比西方自由资本主义更为有效，更为有利可图，怎麽办？若果民主不再是经济发展必然和天生的好拍档，而是其障碍物，又怎麽办？果真如此，那麽在后共产主义国家e，对于资本主义的失望，便不应该轻率地视为人民不了解资本主义真貌，这不是他们对资本主义有着「不成熟」的期望的简单讯号。<br />
<br />
无怨无悔的乌托邦<br />
<br />
当前东欧人民反抗共产主义政权时，大部份人并不是要求以资本主义取而代之。他们希望的是在没有国家的操纵下过自由的日子，随心所欲地相聚谈笑；他们希望过简单而真诚的生活，摆脱脑残的意识型态灌输，{脱人云亦云的犬儒虚伪。许多评论已敏锐地指出，反对者心目中的理想情况，很大程度上是取材于统治者的意识型态。人民所渴望的――以较为恰当的字眼来表达――其实是「具人性的社会主义」。这种态度，值得回眸细看。<br />
<br />
我不禁想起victor kravchenko的一生。这位苏维埃工程师，一九四四年出差到华盛顿是叛变了。他写了一本卖得洛阳纸贵的回忆录《我选了自由》（《i chose freedom》），一本关于史大林恐怖统治的第一身记录，当中包括一九三零年代初于乌克兰的大规模饥荒的详尽记录。当时他还是个真诚的共产主义信徒，也有份协助推行集体化。<br />
<br />
他最为人熟知的，也许是他于一九四九年控告法国一份周刊诽谤，谁周刊指他为酒鬼及殴打妻子，又谓他的回忆录不过是美国情报部的宣传工具。在巴黎举行的诉讼，出庭的包括苏维埃的军官及俄罗斯的农民，一场本是个人的诉讼，竟成为了对史大林统治的控诉。官司他是赢了，但当全世界还在把他捧成冷战英雄时，他却旱地拔葱地抨击麦卡锡的政治迫害：「我深切地认为，与共产主义者及其组织的斗争，绝不能够，亦不应该诉诸共产党人使用的手段及形式。」他警告美国人：「以史大林的手段对待史大林主义，最终只会落得变成无异于自己的敌人」。<br />
<br />
kravchenko 后来亦对西方世界的各种不公平越来越敏感。继《我选了自由》，他写了一本续集《我选了公义》（《i chose justice》）。他在波利维亚实验剥削较少的集体主义，在那e，为了组织贫穷的农民他把钱都花光了。打击太大，他自此退隐江湖。一九六六年，他在纽约的家中吞枪自杀。<br />
<br />
是否离题了？当我们都被二十世纪的共产主义欺骗，复被廿一世纪的资本主义幻灭，我们只能寄望往后浪奔浪流的Kravchenko，与及他们更好的下场。找寻公义，难免由零开始。我们需要论述新的意识型态，当然也难免被贬为危险的托邦，但这不啻会令被支配的绝大部份人从资本主义的幻觉中惊醒。</div>

							
							
							
							
