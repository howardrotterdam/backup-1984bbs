													<h2>波兰禁共产标志 乌克兰拟取缔共产党</h2>
						
						
																											<div id="postmessage_346407" class="t_msgfont">碓矗好之音<br />
记者: 白桦 | 莫斯科 2009年 11月 28日<br />
波兰通过法律，禁止销售、使用和转播共产主义标志。类似的法律已在波罗的海国家实施。乌克兰国内也出现更多的声音，呼吁取缔共产党的活动。<br />
波兰总统卡钦斯基最近签署一项法律，禁止生产、贩卖、使用、传播和存放共产主义以及纳粹法西斯的标志。这项法律两个月前被波兰议会通过。根据这项法律，使用共产党的镰刀、斧头、红旗、红星等标志，将面临罚款或是被判处两年监禁的处罚。但这项法律不扩展到教育、艺术和古董收藏领域。<br />
<br />
*波兰曾深受两个邪恶制度之害*<br />
<br />
波兰议员罗马舍夫斯基在评论这项法律时说，波兰曾生活在纳粹法西斯以及共产主义两个专制制度之间，波兰深受这两个邪恶制度之害。<br />
波兰反对党“法律和秩序”党领导人表示，共产主义同法西斯一样都是种族灭绝的象征，共产主义标志不能在波兰土地上存在。<br />
波兰外长西科尔斯基不久前曾提议，应该拆除位于首都华沙，带有典型的斯大林时代建筑风格的“科学与文化宫”。西科尔斯基说，应在“科学与文化宫”的原址上兴建公园和草坪，让人们野餐。<br />
<br />
*波罗的海国家已通过类似法律*<br />
<br />
除了波兰外，一些波罗的海国家也通过了相类似法律，禁止使用和传播共产主义和法西斯标志。拉脱维亚在上个世纪90年代早已实施类似法律。立陶宛一年前通过和实施的相关法律规定，使用和传播共产主义和法西斯标志将面临150欧元到200欧元的罚款。爱沙尼亚原计划在2007年实施有关法律，但由于当时爱沙尼亚首都塔林拆除苏军青铜战士塑像引起俄罗斯抗议，两国关系紧张，爱沙尼亚当局被迫暂停了有关法律的实施。<br />
<br />
*乌克兰呼吁取缔共产党*<br />
<br />
在另一个已独立的前苏联加盟共和国乌克兰，也出现更多的声音呼吁取缔共产党的活动。乌克兰国家对外情报局档案馆馆长维亚特罗维奇在悼念“大饥荒遇难者日”前夕表示，如果乌克兰共产党把自己当成苏联共产党的继承人，那么从法律和道义的角度来看，乌克兰应该禁止共产党的活动。<br />
<br />
维亚特罗维奇说：“我认为，共产主义同纳粹法西斯一样都是犯罪的意识形态。因此乌克兰应该禁止这样的意识形态。”<br />
乌克兰右翼政治势力“乌克兰民族主义者大会”以及乌克兰西部一些地方的政界人士发表声明说，苏联共产党当年屠杀了千百万乌克兰人，乌克兰共产党是苏共的继承人，乌克兰政府应尽快取缔共产党的活动。<br />
<br />
*总统呼吁同过去苏联划清界限*<br />
<br />
乌克兰首都基辅的纪念碑人权组织领导人克鲁茨克说，在乌克兰仍然能看到许多共产主义标志。乌克兰是否会象东欧国家那样禁止共产主义标志，可能根据形势发展来决定。<br />
克鲁茨克说：“我们暂时还没有象波兰和波罗的海国家那样禁止共产党标志。但我认为，如果乌克兰国内共产党等左翼势力的宣传规模扩大，如果俄罗斯继续限制新闻媒体的活动，并继续对乌克兰施加压力，那么乌克兰可能不得不像东欧国家那样禁止共产主义标志。”<br />
但乌克兰总统尤先科不久前表示，他准备向议会提交法案，禁止共产主义标志。尤先科说，乌克兰必须谴责共产党极权专制并应同过去的苏联划清界限。</div>

							
							
							
							
