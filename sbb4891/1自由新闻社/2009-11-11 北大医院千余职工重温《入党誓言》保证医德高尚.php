北大第一医院官网截图<br />
　　星岛环球网消息：近日，央视报道北大第一医院“非法行医事件”导致北大教授死亡后，引发医学界和公众广泛关注，北大第一医院官方网站对此做出回应，强烈谴责央视报道失实。以下为北大第一医院回应全文：（来源：北大第一医院官网）您怎么看？<br />
<br />
　　北大医院非法行医之说不实 <br />
<br />
　　11月3日21时许，中央电视台2频道（财经频道）“经济半小时”之“半小时观察”栏目播出了一档题为《北大医学教授为何死在北大医院？》的节目，次日，各大媒体纷纷转载，其中多次提到我院“非法行医”，其报道内容失实、断章取义，及其严重地毁坏了我院的名誉，并在社会产生了相当恶劣的影响，令全院教职员工感到无比震惊和愤慨！ <br />
<br />
　　该节目以我院2006年发生的一起目前正在审理的医疗案件为由，对我院进行暗访调查。死者熊卓为系我院心血管内科的一名研究员，因腰椎滑脱在我院骨科住院诊治，由骨科主任李淳德教授主刀成功实施了手术，但术后发生了肺栓塞并发症，经医院全力抢救无效在重症监护室死亡。肺栓塞乃骨科术后卧床可能发生的致死性的并发症之一，而手术本身的成功不能否认。 <br />
<br />
　　在熊卓为发生肺栓塞后，我院立刻调集人员全力抢救，但终因病情过重抢救无效死亡。至于是否应预防性使用低分子肝素等说法，目前在骨科学界尚存在争议，且非诊疗指南的必要内容。新闻记者并非骨科专家，却公然使用“治死”作为主标题内容耸人听闻，有悖于记者的职业道德！ <br />
<br />
　　此外，节目中多次出现“非法行医”或“无证行医”等词汇，亦非属实报道。为熊卓为施行手术的主治大夫李淳德系我院骨科主任，是医院的注册医师，其他相关医师亦不存在“非法行医”的情况，本案涉及的住院医师于峥嵘亦已取得医师资格。根据卫生部对甘肃省卫生厅的“关于非法行医有关问题的批复”（卫政法发[2007]185号）：“已取得《医师资格证书》，并具备申请执业医师注册条件的医师，非本人原因导致未获得医师执业证书前，在其受聘的医疗预防保健机构和工作时间内的执业活动不属于非法行医。”于峥嵘医师当时已经取得《医师资格证书》，所以并不是非法行医，并且在此案审理过程中法庭从未得到“非法行医”的判定结论。这在本案的一审判决中已经有了裁定。 <br />
<br />
　　该节目在本案二审的两天前播出，在宣判前的不负责任的报道将有可能干涉司法，严重影响司法的公正性，反映出个别新闻工作者丧失职　　业道德的现况。我院强烈谴责这种危害社会和谐的无耻行径，呼吁主流媒体应肩负起自己的责任，在利益和良知面前保持清醒的头脑，维护社会的公平正义！ <br />
<br />
　　我院是北京大学的首家综合性教学医院，承担着培养我国医学事业接班人的重要任务，院内有很多医学的研究生跟随具有执业资格的医师学习并参加住院医师培训，研究生不同于普通的医学生，他们都已经取得医学学士学位并正在接受正规的住院医师培训。急诊的刘希高医师已经本科毕业，取得医学学士学位，根据卫生部办公厅关于“正规医学专业学历毕业生试用期间的医疗活动是否属于非法行医的批复”（卫办医发（2002）58号）：“取得省级以上教育行政部门认可的医学院校医学专业学历的毕业生在医疗机构内试用，可以在上级医师的指导下从事相应的医疗活动，不属于非法行医。”刘希高在急诊值班按照医院规定是有上级医师指导的，并不属于非法行医。暗访中违反医院规定未请示上级医师而擅自开具处方乃其个人行为，没有代表性，我院已按规定处理，目前正在进行批评教育。我们在深入了解实情的过程中从刘希高口中得知，在当日暗访时，乔装成病人的记者在提问中存在故意引导答案的嫌疑！其后暗访的即将参加手术的医生也并非主刀医师，以助手身份学习合情合法。 <br />
<br />
