<br />
2009年11月9日上午7时30分，北京大学第一医院（北大医院）第二住院部东广场人头攒动，花坛前悬挂的条幅醒目、积极、庄重:“安全稳定顾大局，全心全意为患者”；“医学教育再创新，辛勤耕耘扶杏林”。全院上千名职工聚集在这里，一次意义非凡的升旗仪式即将开始！院党政领导班子成员全体出席，升旗仪式由党委副书记、工会主席杨柳教授主持。<br />
<br />
北大医院党委书记刘新民教授首先讲话。他说：“<strong>这段时间，北大医院正经历着自建院以来从未有过的痛苦，正在经受前所未有的严峻考验！</strong>” 他鼓舞全体北大医院人“挺直脊梁”，表示“决不辜负党和人民的希望”、“一方面要依照法律提出我们的合理主张，另一方面不要因为失实报道干扰我们的正常工作”。他代表全院党员和职工向党和国家、向人民保证:“要认真贯彻中央领导的指示精神，维护稳定和谐的大好局面，牢记厚德尚道的院训，全心全意为患者服务，为祖国的医疗卫生事业贡献我们的力量！”<br />
<br />
北京大学常务副校长、医学部常务副主任柯杨教授因公不能参加今天的升旗仪式，特委托刘书记向我院全体教职员工和在临床实践中努力拼搏的医学生们致以亲切的问候，向在临床一线一如既往无私奉献的医务工作者致以崇高的敬意！<br />
<br />
在阴霾寒冷的清晨，鲜艳的五星红旗伴着《义勇军进行曲》的铿锵节奏冉冉升起，两面烙印着北大医院“厚德尚道”院训精神的院旗相伴上升。全体北大医院人身着白衣，静静注视着迎风招展的三面旗帜，胸中涌起爱国、爱院的火一般的豪情！<br />
<br />
随后，院党委书记刘新民带领全院共产党员举起右拳重温《入党誓言》，院长刘玉村带领全院医务工作者将右手放在心前重温《医学生誓言》，全体面向党旗和院徽郑重宣誓。“为共产主义奋斗终身，随时准备为党和人民牺牲一切”的伟大志向和“竭尽全力除人类之病痛，助健康之完美，维护医术的圣洁和荣誉”的高尚情怀振聋发聩、声如洪钟！<br />
<br />
北大医院院长刘玉村教授在领誓前讲到:“各位老师:我们都是曾经的医学生；各位同学:你们将成为未来的医生。你们，我们，不论是学生还是老师，大家共同的责任是为了患者的生命和健康。让我们携起手来，为了祖国，为了人民，奉献青春，收获暮年幸福人生！”<br />
