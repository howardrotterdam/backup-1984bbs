													<h2>2009年度代表汉字――凶</h2>
						
						
																											<div id="postmessage_398120" class="t_msgfont">作者：杨莉藜&nbsp; &nbsp; <br />
<br />
2009眼看走到尽头，中国网民们也步起日、台等地的后尘，评选起最能体现2009年的事件、民情、舆情等的代表汉字来。据说，支持率较高的有“被”、“门”、“流”、“具”、“拆”等等。无疑，这些被题名的汉字都在不同程度上反映了2009年中国人的关注热点。不过，要是让笔者也参与一把，我倒是提议把“凶”作为2009年的代表汉字。<br />
<br />
凶，恶也，狠也<br />
<br />
如果拿“凶狠”来刻画2009年中共政权的面目，大致算得上惟妙惟肖吧。当然，中共60年来极少有不“凶狠”的时候，但是，与2008年同比相较，2009年的中共政权之凶狠暴虐格外引人注目。<br />
<br />
2008年，奥运把中国推到了世界前台，中共政权的一举一动都不得不顾忌世人的监督。这一年的人权状况，相比而言，稍稍有所改善。而奥运谢幕，进入2009，中共压抑已久的凶狠本性便开始了剧烈反弹。言论自由方面，中共加强封堵网络，攻击、屏蔽异议网站，力推绿坝软件，试图监控个人电脑和网络。滥用公权，动辄跨省追捕，暴力对付网上言论。网络封锁之剧之烈，为历年之最。不久前，中共公安部部长孟建柱声称：互联网已成为反华势力对中国进行渗透破坏、放大破坏能量的重要手段。这样的言论为他们进一步打压网络言论空间定下了基调。对于传统媒体，中共也再次加强控制，南方报系、《财经》等敢言传媒受到进一步肃整。日前，中国高法下发了《关于人民法院接受新闻媒体舆论监督的若干规定》，强调新闻媒体如果对正在审理的案件报道严重失实或者恶意进行倾向性报道，将依法追究相应责任。这个貌似公正的“规定”，实际上是要让传媒闭嘴。这样的规定，是不是中共当局要为邓贵大报一箭之仇（一般认为，邓玉娇逃脱重判很大程度上有赖于包括网络在内的媒体的力量），实在耐人寻味。<br />
<br />
在对付异议人士和维权人士方面，2009年的中共当局也全然扯下了2008年的虚伪和斯文。本年度，谢长发、郭泉、谭作人、刘晓波等异议人士相继遭到重刑判决。廖亦武、冉云飞、昝爱宗等出境被拦，小乔、冯正虎等入境遭拒。艾未未、刘沙沙、左晓环、王藏等无端遭到殴打……各地访民继续遭到截访机构的关押、殴打和骚扰。女访民遭到准狱吏强奸事件使黑监狱露出了冰山一角，但中共各级政府的暴力截访丝毫没有减退迹象。<br />
<br />
中国的民间组织在2009年也遭到了中共政权的凶狠打压。“公盟”、“益仁平”等团体被取缔，负责人被关押。中国人刚刚争取来的一点点结社自由被扫荡殆尽。<br />
<br />
凶，横也，悍也<br />
<br />
凶横、凶悍，是刻画2009年的中共政权的另一个角度。无法无天、胡作非为本是中共政权的常态，但在2009年，中共的各级官僚把这个特色发挥到了极致。具体表现可以概括为：无视政治伦理，践踏法律法规，罔顾国际批评。<br />
<br />
2009年8月，网上爆出了“老同志”讲执政党政治伦理的谈话稿，一时间，政治（执政）伦理、政治（执政）道德问题成了人们的热门话题。诚如“老同志”所言，缺乏执政道德正是中共政权的一个致命软肋。民众的日益觉醒，使中共意识到自己的执政不合法性的尴尬，因此，2009年新年伊始，当局便通过《求是》、《人民日报》、《解放军报》等官媒刊发了一系列文章，对普世公认的政治伦理进行了轮番批判。多党制、议会制、保障人权、军队国家化等逐个遭到批驳，被指为“西方反动思潮”，号召人民予以抵制。<br />
<br />
中共不仅在理论上蛮不讲理，实践上更是毫无执政道德可言。2009年中共镇压异己的一个主要内容，是打压主张以普世价值立国的《08宪章》的起草者和签署者。“宪章”的主要起草人刘晓波先生被重判11年徒刑，许多签署者和支持者遭到关押、骚扰、审问。中共这样大动干戈的目的只有一个，就是拒绝检讨其败坏的执政道德，继续维护其反文明的一党专制制度。<br />
<br />
执政道德败坏势必导致中共政权为了自身利益粗暴践踏法律法规，使法制建设大幅后退。本年度，中共政权继续拒绝归还宪法赋予公民的各项政治权利，动用国家机器镇压异议人士和维权人士，其蛮横与粗暴为历年所罕见。<br />
<br />
本年度，中共继续以搜刮民脂民膏为乐。中共的无本买卖――地产业，继续成为各级官僚的最大宗进帐，因此，暴力拆迁引发的悲剧此起彼伏。为了夺人家产，中饱私囊，中共官员不惜践踏宪法和《物权法》，逼得屋主自焚、自残、跳楼，有的地方官员甚至指挥拆迁队开车碾压拒迁者、焚毁屋主房屋、殴打拒迁家庭妇孺。<br />
<br />
2009年可以说是中共政权狰狞对抗国际文明力量的一年。中共投入了数以亿计的资金争夺国际话语权，推销其独裁专制理念和以低人权优势换来的经济发展模式。<br />
<br />
面对国际社会对中国人权问题的批评，中共要么反唇相讥，要么依然故我。这在新疆骚乱、刘晓波案等国际关注的热点问题上表现得至为突出。<br />
<br />
凶，灾也，死也<br />
<br />
与2008年相比，2009年虽然没有汶川地震那样震惊世界的凶灾，但各种凶馑持续不断，祸患波及整个中国。中国多年畸形发展，欠下了无法偿还的环境债。本年度暴冷暴热、旱涝接踵的极端天气此起彼伏。血铅儿童、放射源泄露等严重环境事件在多省区发生，中国人的生存安全已是岌岌可危。<br />
<br />
本年度，持续的金融危机重创了中国以出口加工为主的低端产业，大量农民工失业。虽然官方自称中国已经率先走出经济危机，且GDP达到预计增幅达10％左右，但民众的实际体验比这些官方数字更具说服力，民间流传的“被增长”、“被小康”无情拆穿了中共长期乐此不彼的数字游戏。<br />
<br />
2009年，中国各级官僚依旧追逐带血的GDP的增长。根据国家安全监管总局的统计，仅在上半年，大陆共发生各类事故186775起，死亡36370人。这个数字在下半年至少将会翻番，超过2008年的死亡总数。8月份，山西省开始进行煤企重组，让煤老板退出历史。但这种国进民退的整改并不能从根本上改观中国草菅人命的权贵资本主义膨胀，事实上很多造成重大事故的企业，如臭名昭著的三鹿奶业，本身就是国有或国资。<br />
<br />
面对甲流的传播，中共政权没有拿出一个负责任的政府应有的诚意和措施。甲流初期，中共沉醉于作秀式防控，爆发期则为了60年庆典而瞒报、漏报，或干脆不报。十月份之后，在世界卫生组织的批评和国内舆论的压力之下，才披露了真实的感染和死亡数字，而此时的甲流早已在全国遍地开花，所谓的防控措施已是形同虚设。<br />
<br />
2009年7月，中共长期的不合理少数民族政策终于引发了新疆骚乱，中国官方统计为千余人死伤，实际数字外界不得而知。事件发生后，中共政权对政策和制度性问题鲜有反思，反而加大对维族人的打压力度，这为进一步的民族冲突引燃了导火索。<br />
<br />
凶，不祥，不吉也<br />
<br />
官权无羁，腐败丛生，人权委尘，民怨鼎沸……所有这一切都显示出国运民生的暗淡前景。<br />
<br />
中国传统的辩证思维有否极泰来之说，具体到国运方面，就是所谓治乱相循，祸福相伏。中共政权在2009的凶横跋扈和民众的饱受践踏，可以说已经达到了常人所能想象的极限。然而，否极矣，泰来否？<br />
<br />
就目前的现状观之，至少眼下还看不到任何“泰来”的迹象。中共政权专横跋扈，拒绝改革，以国家暴力对待和平的变革诉求。然而，强力压制只会带来强力反弹――日积月累的民怨最后只会汇成喷薄的岩浆，劈头盖脸的打击连改革者也会被“锤炼”成革命者。到那时，曾经不可一世的独裁者们绝不会“死得很舒坦”（借用2009年最后一句雷人语），而更有可能像20年前的齐奥塞斯库那样“死得很狰狞”。<br />
<br />
那样的结局，不论对现政权，或是对饱经离乱的中国人，都不算一种吉兆吧。</div>

							
							
							
							
