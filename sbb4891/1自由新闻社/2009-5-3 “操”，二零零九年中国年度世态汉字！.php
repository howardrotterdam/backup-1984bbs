																											<div id="postmessage_86050" class="t_msgfont">“操”，二零零九年中国年度世态汉字！ <br />
<br />
王健&nbsp;&nbsp;2009-5-3 17:58&nbsp;&nbsp;<br />
<br />
日本每年都会评选一个年度世态汉字，07年的“伪”，让日本人感到了羞愧；08年的“变”，让日本人充满期待。<br />
<br />
奇怪的是，真正使用汉字的国家――中国，却从来没有搞过类似的评选，这心里还真有一点不服气。端午节的风头已经被韩国人抢去了，这汉字的风头再被小日本抢去，我等“粪粪们”岂肯忍之？<br />
<br />
好在日本的年度世态汉字评选时间并不长，是从1995年开始的，我们完全可以迎头赶上，由于中国08年发生的事情比较多，也有代表性，就从08年开始最合适，8代表发嘛。<br />
<br />
我认为08年最具影响力的汉字，应该是“灾”，关于“灾”字的解释，百度百科有如下条目：1、灾害,祸患；2、天灾降戾；3、人君失政,天为异;不改,灾其人民;不改,乃灾其身也；4、暗示强烈的、痉挛的或突然涌起的混乱行动DD如在地壳中、个人思想上或国家中灾害。想想将要到来的“5.12”周年祭日，恐怕没有人会反对把“灾”作为08年中国年度世态汉字吧。但是且慢，就在余大师含泪劝告过我后不久，八月份张大师就强行改成“和”了。和就和吧，不和不行呀，不承认“和”的人，教授很生气，后果很严重，99%的人成了神经病，剩下1%的人在躲猫猫。网络上也一遍“和”气，高举“反低俗”的大旗，关闭了2962家非法网站（截止到09年2月24日）。有人认为，这些被关闭的网站，“淫不淫”不重要，关键是是否同意“和”，否则无法解释一些喜欢“媾和”的网站依然大摇大摆，而不同意“和”的网站――如牛博网，却只能流亡海外。还不信？那你就到百度里面“操”一下，看看结果是什么。先声明，你要是操到吐血可不关我的事，是百度的事。<br />
<br />
说曹操，曹操到。虽然２００９年刚过了四分之一，但是我根据中国整体发展趋势作了一个预测，本年度中国世态汉字就是“操”。<br />
<br />
各位看官，听我说说理由，同意的顶一顶，不同意的拍拍砖，但是请不要脏口，注意网络操守。<br />
<br />
首先是年初网上一首红遍大江南北的《草泥马之歌》，为“操”字作了很好的铺垫――当然，你说前戏也行。虽然“草”和“操”字不同、意也不同，但近似的发音，还让大众熟悉了“操”这个字。其次是最近特区深圳发生的一个故事，将“操”字带到了高潮。报载：深圳一陈姓公民因不服一审判决提出“上诉”，但因在上诉书“事实和理由”一栏中仅写了一个“操”字，结果被深圳福田区法院作出拘留十五天的决定。<br />
<br />
乖乖隆里冬，一个“操”字和十五天的人身自由捆绑在一起，你说这字厉害不厉害？说实话，我猜不出深圳陈公民是在怎样的心情下写了这个字的，我想：如果他是一边听《草泥马之歌》，一边在思考如何填写上诉书的“事实和理由”，可能他写的就不是一个字，或许他会填上一个成语：卧槽泥马。<br />
