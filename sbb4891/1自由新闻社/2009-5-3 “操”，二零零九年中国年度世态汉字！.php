<br />
报道称，日本汉字检定协会11日在京都的清水寺公布2009年日本年度汉字为“新”字。清水寺的住持森清范书写了“新“字。<br />
<br />
今年的汉字是日本汉字检定协会从1万多封来稿中选出了“新”字。因为今年日本发生了“新政权诞生”、“新型流感”、“体育界又产生新纪录 ”等事情，所以推选“新”字的呼声最高。<br />
<br />
接下来呼声比较高的是“药 ”、“政”、“病”等汉字。<br />
<br />
日本选出“今年的汉字”活动是从1995年开始。现在已经成为日本每年的一个惯例活动。2008年的汉字是“变”，因为2008年发生了世界经济大变样、美国黑人总统上台、日本首相更换、日本物价上升等事件。<br />
