													<h2>湖南武冈详解副市长自杀案:割腕触电再跳楼</h2>
						
						
																											<div id="postmessage_350980" class="t_msgfont">新文化报12月2日报道 湖南武冈市常务副市长杨宽生11月26日非正常死亡后，湖南省官方称经确认杨系因精神抑郁自杀身亡。昨日，武冈市政府发言人曾昭训、武冈市公安局局长康文祥分别接受了记者的采访，详细解释了杨宽生属于自杀死亡的结论。<br />
<br />
不是死于官场暗斗或仇杀<br />
<br />
武冈市政府新闻发言人、政府办公室副主任曾昭训说，家属之前对死因有异议，“是在尸检报告出来之前”，尸检结束以后，“主要家属成员已经认可了尸检结论”。<br />
<br />
此前，曾有传闻称参加尸检的医生称“不可能属于自杀”。但据记者了解，该说法并非出自医生之口，而是杨家属向媒体转述后产生的误解。<br />
<br />
据了解，鉴于杨宽生的特殊身份，湖南省、邵阳市极其重视此事，成立了包括省市公安部门刑侦及痕迹鉴定专家、权威法医等成员的调查组。据曾昭训介绍，调查组在经过侦查、走访及尸检等程序后，确认了杨宽生系精神抑郁自杀身亡。曾昭训说，作为一个地方高官，杨的死因民间有各种议论是正常现象，但官方的结论“是科学的、严谨的”。<br />
<br />
杨为何精神抑郁，曾昭训称不能妄加判断，但有一个细节如今可供参考。曾说，杨在11月22日曾参加一个研究当地高速公路的市政府办公会议，整个会议杨宽生“一言不发”。<br />
<br />
对于坊间有传闻杨或许死于官场暗斗或仇杀，曾昭训给予了否认，称这只是一些群众毫无依据的猜测。<br />
<br />
定自杀：“这是个铁案”<br />
<br />
武冈市公安局局长康文祥向记者介绍，该案已经侦查终结，其结论“可以经得起历史的检验”，“可以说，这是个铁案”。<br />
<br />
据康文祥介绍，排除杨宽生“他杀”的可能，主要基于以下一些侦查证据：首先，杨宽生在武装部生活区内的寓所门窗完好，并无他人攀爬或入室等痕迹。在杨的房间内，“除了跟杨宽生鞋子一样大小的鞋印外，再无其他脚印痕迹”。<br />
<br />
其次，杨的房间内并无任何搏斗痕迹。“如果是他杀，肯定会产生搏斗”。从尸检情况看，杨宽生颈部、手腕确实有刀痕，但经过检验“属于他试探性自杀留下的痕迹”。康说，警方侦查专家推断，杨可能先选择以割脖子的方式自杀，但后来选择了割腕。而手腕处的伤痕，就是杨多次割腕的结果。<br />
<br />
此外，康文祥亦向记者透露，杨宽生确实留下遗书(以前媒体曾报道，杨的一位外甥称，听说杨死后留有一份未公开的遗书，上面有“我没有收礼金”，“有一个邵东人要害我”)。该遗书是杨在心态正常的情况下写的，“经鉴定专家认定，并没有受到任何胁迫”。据悉，杨宽生遗书的最后一句话是“可怜父母亲、妻儿，我走了。”<br />
<br />
自杀时使用了三种工具<br />
<br />
康文祥称，据警方侦查结论，杨宽生自杀时使用了菜刀、水果刀和剪刀三种工具;此外，为了了结性命，其采取了触电、割腕和跳楼的方式。尝试了种种自杀方式之后，杨最终从三楼坠下身亡。<br />
<br />
<br />
康文祥向记者介绍，从现场及尸检情况判断，杨宽生在选择自杀时，最先用刀割颈部和左手上手腕，但最终未自杀成功。随后，杨右手持刀割破左手下手腕，应该连续割了几刀，再用剪刀剪断动脉，这也是杨“右手沾满血迹，而左手却没有多少”的原因。据悉，杨在割破手腕后，还尝试了触电自杀的方式，但并未成功。<br />
<br />
据警方的调查结论，杨在大量流血虚脱之后，在一段时间内并未死亡。这可能是其选择跳楼的原因。据康文祥介绍，从现场血迹判断，杨宽生第一次并未翻过阳台，在掉回阳台内侧后，杨第二次攀爬时才从阳台坠下。“在下坠时，还被二楼的遮阳板刮到”，这也是其肩部淤血的原因。<br />
