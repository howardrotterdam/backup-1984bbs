													<h2>武冈市副市长“被自杀”疑点重重，家属已逃离湖南伸冤</h2>
						
						
																											<div id="postmessage_386123" class="t_msgfont">武冈市副市长“被自杀”疑点重重，家属已逃离湖南伸冤<br />
　　<br />
　　文/周筱S<br />
　　<br />
　　<br />
　　据本人几个小时前获得的消 息，被当地警方认定为自杀的湖南武冈市常务副市长杨宽生，其妻刘月红已经离开湖南，将向公安部及最高检提请重新调查。昨日，刘月红对记者表示：“如果人民政府不能为我伸冤，我只有以死抗争。”为了刘月红的人身安全考虑，我暂时不能透露刘月红的暂住地。<br />
　　<br />
　　11月26日，湖南省邵阳市下 辖的武冈市，常务副市长杨宽生死于寓所楼下。随后，官方认定杨系“精神抑郁自杀死亡”。此事经媒体披露，引起全国舆论的关注。据武冈市公安局局长康文祥称：“杨宽生自杀时使用了菜刀、水果刀和剪刀三种工具；此外，为了了结性命，其采取了触电、割腕和跳楼的方式。尝试了种种自杀方式之后，杨最终从三楼坠下 身亡。”<br />
　　<br />
　　如果真的如武冈市公安局局长所说，那么武冈市常务副市长杨宽生采取多种自杀方式，是抱着必死的决心的，然而，据刘月红 说，出事前的周末，她曾到武冈为丈夫清扫寓所、洗衣服，没发现丈夫精神状态有任何异常，“相反，他的心情很好，因为在10月底邵阳市做的民意测评中，他在武冈市两次排名第一”。也就是说，不出意外，马上就可以升职了。当地因此风传，杨宽生得罪了当地的权势集团而遭到谋杀。<br />
　　<br />
　　武冈市官 方可以说这是他自杀前的掩饰，虽然真的决心自杀者，不可能没有一点迹象，当然，也不排除杨宽生演技一流的可能，然而，最大的疑点在于，据刘月红向记者的最新披露，11月25日晚9时30分，也就是杨宽生“被自杀”前一天，打给她一个电话。“他说有人要合伙害他，他的手机和我的手机都被监控了，后来又说没 事，明天他要回邵阳。”<br />
　　<br />
　　电话记录在运营商全部可以查到，如果刘月红说了谎，马上可以证伪，所以，刘月红在这个问题上不可能说谎。<br />
　　<br />
　　当地公安部门出具的《关于杨宽生同志死亡结论的意见书》和尸检报告，排除杨宽生排除他杀依据中说，经勘查，现场血迹均为滴落状、流注状血迹、血泊，无喷溅状血迹，血迹滴落自然；地面只有一种血脚印，排除第二人在场。<br />
　　<br />
　 　然而问题在于，这完全是睁眼说瞎话！刘月红向记者展示了他们12月5日在现场拍的照片，完全与意见书所说不符合。照片显示：整个住所，除杨宽生平时睡觉 的大卧室，其他房间血迹四处可见，其中电视柜、空调、饮水机、客厅门背后等至少5处有“明显的喷溅状”血迹，另外还有不下十处“涂抹状”血迹。<br />
　　<br />
　　这与意见书上称现场无喷溅状血迹明显与事实不符。但凡看过凶杀片的人都知道，喷溅状血迹是判断属于他杀的重要依据。<br />
　　<br />
　　现场除杨死时所穿布鞋留下的圆脚印外，还发现一个尖脚印。这个现场的第二个脚印到底是谁？难道照片也是死者家属伪造的？如果现场遭到破坏，再要重新勘测就不可能了。<br />
　　<br />
　　此前的报道都没有提及杨宽生妻子刘月红的职业，她是一名外科医生，凭借自己的专业知识，她断定自杀不可能造成丈夫那些伤痕。坠楼后最早到现场的目击者称，地面上只有两滴血，尸检过程中也只在尸体上发现很少血，这与坠楼自杀特征明显矛盾，反而更与濒死前被扔下相符。<br />
　　<br />
　　既然官方认定是自杀，就不属于刑事案件，并称现场发现了遗书，那么遗书就应该交还家属，但刘月红向公安部门多次索要杨的遗书遭到拒绝，武冈市公安局局长必须经过上级领导同意。为什么不敢把遗书给家属看呢？到底想掩盖什么呢？难道怕家属认出笔迹不对吗？<br />
　　<br />
　　<br />
　　“家属与律师提出的疑点”<br />
　　<br />
　　 ●本案尸检报告11月28日做出，死亡结论意见书却是11月27日就已做出；<br />
　　<br />
　　 ●现场有存在第二人的重大线索；<br />
　　<br />
　　 ●意见书只有武冈市公安局的公章；<br />
　　<br />
　　 ●意见书上称现场无喷溅状血迹明显与事实不符；<br />
　　<br />
　　 ●楼下邻居证实，当晚有捶墙、跺地板及挪动椅子声响，但意见书称邻居证实没有异常声音；<br />
　　<br />
　　 ●既然意见书认定“自杀”，为何不将作为死者遗物的遗书归还家属；<br />
　　<br />
　　 ●杨宽生死亡前夜，其妻接到他的电话说“有人要害我”，这一重大疑点被警方忽略；<br />
　　<br />
　　 ●现场只有书房一盏台灯开着，其他灯都关着，一个高度近视者如何自杀后还在黑暗中乱走；<br />
　　<br />
　　 ●意见书称“左手腕伤口方向平行”，“他人无法完成”，但尸检报告写是“左腕部创口共有7处创缘叉口”，家属看到的是后者；<br />
　　<br />
　　 ●坠楼后最早到现场的目击者称，地面上只有两滴血，尸检过程中也只在尸体上发现很少血，这与坠楼自杀特征明显矛盾，反而更与濒死前被扔下相符；<br />
　　<br />
　　 ●尸检报告称死者全身是血，如果切腕自杀，背部血迹又来自何处，死者不可能故意躺在地上将血弄得到处都是，这不符合自杀的逻辑；<br />
　　<br />
　　 ●死者几处肋骨骨折的方向符合死者死前受外力打击所致，恰恰与认定的面朝上坠楼形成的骨折方向相反；<br />
　　<br />
　　 ●尸检报告称死者身体见几处片状青紫，但未解释这些伤的原因，意见书认定“自杀”在前，所以法医已经无法再形成独立判断。<br />
　　<br />
　　<br />
　　<br />
　　以下为记者卢斌的报道<br />
　　<br />
　　副市长非正常死亡 家属提请重新调查<br />
　　湖南武冈市警方认定其系自杀，称三级公安联合调查结果“经得住历史检验”<br />
　　<br />
　　湖南武冈市常务副市长杨宽生非正常死亡，当地警方认定系自杀，家属不服将向公安部及最高检提请重新调查。昨日，杨宽生之妻刘月红表示：“如果人民政府不能为我伸冤，我只有以死抗争。”<br />
　　<br />
　　11月26日早晨6时，杨宽生被人发现躺在武冈市武装部宿舍楼下，送医院后不治身亡，武冈市公安局当天即对尸体进行了检验。<br />
　　<br />
　　共有5名杨的家属见证了尸检，其中包括两名医生，家属据此有足够的自信对目前官方的结论提出质疑。<br />
　　<br />
　　刘月红本人也是名外科医生，刚开始她除了痛哭外只有沉默，当时其他家庭成员则在抗争，本定于12月1日的下葬因此没有成功，令赶到杨宽生老家新宁为其送行的武冈官员十分尴尬。最终，相关人员做通了刘月红的工作，杨宽生于12月4日入土。<br />
　　<br />
　　妻子：警方死因认定“错漏百出”<br />
　　<br />
　　一度有家属称，刘月红或许埋藏了丈夫的隐秘。<br />
　　<br />
　　杨宽生死得突然，刘月红昨日对记者说，她受打击太大，在头脑混乱情况下同意下葬。“后来头脑慢慢清醒了，发觉不对。”凭借自己的专业知识，她断定自杀不可能造成那些伤痕，于是聘请了律师“我下决心为他讨一个说法。”<br />
　　<br />
　　12月5日，丈夫下葬后第二天，刘月红向公安部门索要了《关于杨宽生同志死亡结论的意见书》和尸检报告，并与亲属去到事发现场拍了照片。随后，在律师的帮助下，他们研究了这两份官方材料，认为警方对死因的认定意见“错漏百出，疑点重重”。<br />
　　<br />
　　11月27日晚10时许，武冈市公安部门就向家属宣读了这份意见书，湖南省的宣传部门几乎在同时也发布了杨宽生死亡的结论。不同的是，公布的新闻稿中提到杨宽生系“精神抑郁自杀身亡”，但意见书中并未见类似表述。<br />
　　<br />
　 　杨宽生调到武冈后，夫妻平时分居两地，在刘月红看来，丈夫是一个十足的“工作狂”，除了春节之外几乎没有休息过。刘月红说，出事前的周末，她曾到武冈为 丈夫清扫寓所、洗衣服，没发现丈夫精神状态有任何异常“相反，他的心情很好，因为在10月底邵阳市做的民意测评中，他在武冈市两次排名第一。”<br />
　　<br />
　　妻子：他说有人要合伙害他<br />
　　<br />
　　唯一让妻子感到异常的，是11月25日晚9时30分，杨宽生打给她的那个电话。“他说有人要合伙害他，他的手机和我的手机都被监控了，后来又说没事，明天他要回邵阳。”刘月红说，杨第二天还约了人签字，看不出任何自杀的迹象。<br />
　　<br />
　　让家属们最不能容忍的是，他们向公安部门多次索要杨的遗书遭到拒绝，“一眼都没让我们看”。家属们从最早进入现场的人员那里得知，遗书大致写道“我没犯错误，没有收他们的红包礼金，我不想死，想搞死我的人是邵东人。”<br />
　　<br />
　　对此，刘月红的律师滕彪表示，武冈市公安部门的做法相互矛盾，既然认定是自杀，案件便不再属于刑事案件，就没有任何理由不把遗书给家属。<br />
　　<br />
　　公安局长：我无权给家属看遗书<br />
　　<br />
　　武冈市公安局局长康文祥告诉记者，遗书有写给组织的遗书，也有给亲属的遗书，杨的遗书属于前者，现在保存在案卷中，就他本人来说，给家属看未尝不可，但是他没有这个权力，必须经过上级领导同意。<br />
　　<br />
　　通过对照意见书和尸检报告，家属与律师还发现一个十分蹊跷的地方：尸检报告的落款日期为11月28日，但是死亡结论意见书却是前一天11月27日就已做出，即是“先有死因结论，后有尸检结果”。滕彪认为，这至少在程序上极不合理。<br />
　　<br />
　 　杨宽生的家属还称，在公安公布正式结论之前，11月27日中午，武冈市委书记周晓红就已对家属表示，死因“偏向”自杀，当日下午便有当地官员来做家属思 想工作，让他们接受自杀的现实，而且还提到现场发现“一些对杨宽生不利的证据”，包括被烧毁的现金和银行卡，之后接连不断来做工作的很多官员都提到此类细节，其中包括杨生前的好友、同学、老领导、老乡。“他们说要顾及杨宽生的形象和对他儿子的影响。”<br />
　　<br />
　　康文祥解释，尸检报告的落款日期为11月28日，并不等于尸检结论那天才得出。11月27日，调查组已经知道了尸检结果，并认定了死因。家属催得急，所以就先把结论通知了他们，而当时已近深夜，所以尸检报告还未完成一些程序。<br />
　　<br />
　　至于案发现场是否发现对杨宽生的“不利证据”？康文祥称“这方面的事情不好说，我只能说他的死亡排除他杀。”<br />
　　<br />
　　意见书排除他杀依据包括，经勘查，现场血迹均为滴落状、流注状血迹、血泊，无喷溅状血迹，血迹滴落自然；地面只有一种血脚印，排除第二人在场。家属昨日向记者展示了他们12月5日在现场拍的照片，表示意见书明显与事实不符。<br />
　　<br />
　　律师滕彪表示，整个住所，除杨宽生平时睡觉的大卧室，其他房间血迹四处可见，其中电视柜、空调、饮水机、客厅门背后等至少5处有“明显的喷溅状”血迹，另外还有不下十处“涂抹状”血迹，现场除杨死时所穿布鞋留下的圆脚印外，还发现一个尖脚印。<br />
　　<br />
　　“不相信县公安局，也得信省公安厅啊”<br />
　　<br />
　　关于杨宽生的死亡过程，警方的推断十分曲折、惨烈，先后使用了菜刀、剪刀和水果刀抹颈、割腕、血流一地之后又触电未果，最后选择了跳楼。<br />
　　<br />
　　滕彪称，现场血迹要数客厅中的一处人形血泊最大，长1.3米、宽0.5米，杨已经流了大量血，若再加上房内各处血，人不死也是休克状态。“怎么还可能翻越阳台栏杆跌落楼下？”滕彪表示，意见书上的现场勘查描述极为粗略。<br />
　　<br />
　 　“你不相信县公安局，你也要相信市公安局啊！不信市公安局，也得信省公安厅啊！”康文祥昨日再次重申，杨宽生死因是湖南省、邵阳市及武冈市三级公安部门 联合调查的结果，“经得住历史检验”。关于现场勘查细节的疑问，他说电话中无法向记者解答，只有到现场实地讲解，不能仅仅根据照片进行推测。“我说一个脚印，你非说两个，这怎么行。”康文祥还强调，现场至今没有被破坏，他们保存完好。<br />
　　<br />
　　另外，结论为三级部门联合作出，但意见书上却只有武冈市公安局的公章。康文祥解释，因为主办案件的还是武冈市公安局，所以通知家属的意见书自然以该局的名义。<br />
　　<br />
　　律师：只能寄望于引起上级重视<br />
　　<br />
　　家属提交申请究竟是否能促成该案重新调查？滕彪表示，因为死因认定为自杀，所以能走的法律程序较刑事案件少得多，本来重做尸检如找到他杀证据，便可以让公安部门立案，但从技术上已无可能，如今只能寄望于引起上级部门重视。<br />
　　<br />
　 　滕彪还表示，国外的经验是，一些国家碰到死因有争议的案件，有专门的死因法庭可以裁断。比如，杨宽生的死亡，家属和刑侦机关都可以在法庭上陈述各自的理 由，进行交锋。有的国家即使没有死因法庭，也有类似机制。我国法律在这一方面，需要完善的地方还很多，所以一旦出现“被自杀”事件，老百姓很难找到申诉的渠道。<br />
