杨宽生系湖南省邵阳市绥宁县人，根据武冈市政府门户网介绍，杨宽生协助市长处理市人民政府日常事务工作，分管政府办、应急管理、发改、统计、物价、审计、公安、司法、监察、人事、编办、财政、税务、矿产税费征收、重点项目调度、劳动保障、残联、农业综合开发、特色产业办、公积金中心等工作，以及对外联络和联系检察机关、法院等工作。有来自当地政府工作人员的消息显示，杨生前为人厚道，工作也很称职。<br />
<br />
记者通过当地媒体获知，杨宽生具体死亡时间为26日凌晨4点左右，当时杨正在其人武部宿舍楼的家中休息。“我当时正在睡觉，突然听到一声物体高空坠落的声音。”人武部宿舍楼一不愿透露姓名的群众告诉记者，他和杨宽生住在同一栋楼，当他听到响声起来查看情况时，发现杨宽生已从其所住的四楼坠落下去。后有群众认出了杨宽生，并拨打了120和杨宽生秘书的电话。<br />
<br />
而根据当时进入杨宽生房间的群众表述，他们发现杨家房间大门洞开，地上布满血迹。另从杨宽生的秘书处了解到，杨宽生25日下午才从邵阳市赶回武冈，当时表现并无异常。<br />
<br />
记者多方了解后获悉，杨宽生死亡前几天一切工作生活都很正常。24日，武冈市召开市长办公会，杨宽生参会，会议开了2个多小时，未现异常。25日，杨宽生在邵阳市开会，会后司机把杨宽生送回武冈，等杨宽生回到家后，司机回家。25日晚上9时，杨宽生告诉妻子，武冈有两个人在等他，并称有人想害他，怕妻子担心，后来又告诉妻子“不要紧”。25日晚12时左右住一个单元的邻居听到有人在喊杨宽生的门，随后听到杨宽生开门的声音。之后便发生了让人们意想不到的一幕。<br />
<br />
家属拒签尸检报告<br />
<br />
<strong>对于“自杀”结论，杨宽生的家属并不认同。根据尸检结果来看，杨宽生腰部有两大块淤血，后背有一个人的手印，而且杨宽生手腕被割断，颈部有三四处刀痕。据此，家属认为杨宽生的死亡原因乃他杀。</strong>因此，截至目前为止，家属并没有在杨的尸检报告上签字。<br />
<br />
对于家属的这些疑问，邵阳市和武冈市相关负责人有着自己的解释：这些伤都是杨宽生在跳楼前自己所为，他割腕后跳楼，在送往医院抢救时不治身亡，而杨宽生患精神抑郁已有半年时间。<br />
<br />
让人觉得蹊跷的是，记者29日下午先后登录湖南省、邵阳市以及武冈市政府门户网站，先前发布的“自杀结论”通告已难寻踪迹。通过对“杨宽生”的搜索，也仅仅显示一条他2004年当选“湖南省再就业工作先进个人二等功”的消息，其他再无信息。<br />
<br />
<br />
<br />
此外，在浏览武冈当地多家论坛后，记者发现，杨宽生在当地群众中有着不错的口碑。可惜，47岁的生命意外划上终止符，当地政府的态度却是“结论已下，自杀是个人行为，不是刑事案件，没有继续调查的必要”。<br />
<br />
新闻回顾： 湖南武冈副市长凌晨坠楼身亡 被疑为他杀<br />
<br />
26日凌晨湖南省武冈市常务副市长杨宽生从其所住的四楼坠落死亡。当地公安部门不愿透露详情。而其家属称杨宽生腰部有两大块淤血，后背有手印，且手腕被割断，颈部有3、4处刀痕，认为杨宽生系他杀。<br />
