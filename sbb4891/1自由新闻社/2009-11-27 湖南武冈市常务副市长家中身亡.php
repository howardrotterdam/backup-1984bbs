																											<div id="postmessage_397483" class="t_msgfont">武冈副市长坠楼整一月 警方称从宿舍搜出21万<br />
<br />
2009年12月26日09:46<br />
<br />
　　本报讯 今天，刚好是湖南武冈市常务副市长杨宽生坠楼身亡一个月。昨天上午11时，杨妻刘月红从武冈市公安局局长康文祥手里拿到了丈夫的遗书复印件。遗书原件则继续由公安局保留，此前康给出的原因是“遗书大部分内容是向组织交代情况”。<br />
<br />
　　在武冈市公安局讨要遗书时，刘月红还见到了杨宽生的生前好友、老家新宁县的主要领导以及刘月红所在医院的两个院长。他们劝刘月红收手，但刘月红表示：“下定决心做到底。”<br />
<br />
　　另悉，24日，武冈市委组织部长夏贤钦证实，市长申建伟和市委书记周晓红即将调往邵阳市，分别出任农业局局长和市委副秘书长。<br />
<br />
　　刘月红：拿到遗书复印件疑伪造<br />
<br />
　　昨天下午，刘月红等人拿着复印件找到曾经在第一现场看到遗书的人，让他辨别真伪。<br />
<br />
　　该见证人告诉刘月红，他看到的遗书内容是之前网络上公布过的版本：“我没有犯错误，没有收他们的礼金红包，想搞死我的人是邵东人，我不想死，父母妻儿我走了，是邵东人逼死我。”<br />
<br />
　　现在的遗书内容则是：“我没有多大过错，我工作很忙，收到的红包礼金没有空去存，工资卡也没取，父母妻儿我走了，我不想死，是邵东人逼死我。”该见证人称，他看到的遗书有两三行内容，但刘月红拿到的却有5行。<br />
<br />
　　邵阳警方：武冈太穷 没钱栽赃<br />
<br />
　　又讯 昨天，湖南武冈市警方还将杨宽生的手机、笔记本等遗物交还给家属，并首次表示，在杨宽生宿舍中搜出现金21万余元。据参与全程侦查取证的邵阳市公安局刑侦支队肖政委称,一直未公开调查结果，是因为杨宽生在老百姓中有很好的名声，怕对其名誉造成不好的影响。<br />
<br />
　　杨宽生的家属则表示，21万元的现金可能是有人栽赃陷害。邵阳市公安局技术大队尹队长称：“说句玩笑话，武冈都这么穷了，谁还有钱拿20多万元来栽赃副市长？” <br />
