本来，在十八大人事布局前夕，紧跟江系上海帮的广东本土政商势力被作为团派势力攻城略地、敲山震虎的突破口，从广东籍的最高院副院长黄松有、公安部经侦局长郑少东、广东“政法王”陈绍基、黄丽满提拔的深圳市长许宗衡一一被拿下，团派通过中纪委副书记何勇高举反腐大旗打击上海帮等政敌，可以说是所向披靡，团派的汪洋大有成为“新粤王”之势，江势力节节败退，民间甚至屡屡传出上海帮大将周永康被双规、年迈恋栈的江泽民在湖南火车“人为”对撞中被重伤的传闻，似乎已经树倒猢狲散，团派已经稳操胜券，今后只要与太子党势力进行权力分赃即可。然而，狗急也会跳墙，如何扭转不利局面、稳定原派系内的全国官员军心就成了上海帮的当务之急（也是原广东本土官员派系反击钦差大臣汪洋的需要），要力挽狂澜的唯一途径只有利用偶然发生的特大事件或者人为制造特大事件了，在枪杆子里出政权的传统中国，只要抓紧军警的大部分兵权，就能确保自己的红旗和江山不倒。而最近广东韶关的维汉工人冲突，延烧到今日的新疆乌鲁木齐维吾尔族群众暴动（因为尚缺有力证据，记者现在还无法证实维汉冲突是某些政客幕后导演的），偶然发生也好，人为操纵故意引发的也好，恰恰为江系上海帮反扑创造了一个反扑良机。<br />
本来，隶属江系的王乐泉在天高皇帝远的边疆通过多年铁腕统治已经成了名符其实的“新疆王”，加上插手资源大省新疆各种矿产资源的分配掠夺，与广汇集团孙广信等山东籍老板的官商一体化让王乐泉家族拥有天文数字的个人财富，新疆武警支队更是升格为副军级，政权、兵权、财权统统在手使王乐泉渐渐做大成尾大不掉之势，成为政治局中谁也不敢小觑的实力派人物，如此“结私朋党”不要说现在一党专政的中国，就是以前各个皇朝时代都是皇帝非常忌讳的眼中钉。团派派到新疆的新锐副书记胡伟“潜伏”多年却象小媳妇，连自治区常委都不是（这也可见王乐泉根本不把团派放在眼里），更不用说取代王乐泉了。知情人士向记者透露，团派早就在收集王乐泉在经济方面的黑证据，通过反腐战击倒王乐泉指日可待，至少可以十八大前后换掉王乐泉，给他一个全国人大或政协的虚职，如果新疆能持续保持和平稳定，让王乐泉被迫离开油水多多的新疆是团派胜算在握的。 (博讯 boxun.com)<br />
如今可好，信奉枪杆子里出政权的江系上海帮最怕国内外平安无事、最怕经济挂帅取代政治挂帅，那样会让武装力量重要性降低，减少自己在政治局里的话语权，因此所谓“国内外敌对分子”的老调重弹是上海帮所乐见的，这也算唇齿相依的辩证法了。而新疆暴动看似之发生在乌鲁木齐，其实牵一发动全身，因为军警必然要在维吾尔族人口聚集的例如伊犁、喀什等地加强布防力量，以便防止第二波暴动和缉拿在逃的暴动骨干力量，公安部门就不必说了，整个兰州军区和附近几个省武警部队的布防必然都会做出调整，只要兵马一调度，周永康和江系在军中的代表人物重要性大增，立马重振旗鼓，让人马士气大振。如果该事件越闹越大，那自然会追究到事件源头广东韶关的领导责任，雄心勃勃的政治局委员、团派大将广东省委书记汪洋自然脱不了干系，他在十八大后迈进政治局常委的仕途之路徒增艰险，这也是江系这次反击团派的战果之一。<br />
新疆暴乱后续事件如何发展，是否会改变目前中国政坛的权力布局，我们将继续追踪报道。</div>

							
							
							
							
