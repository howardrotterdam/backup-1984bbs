<br />
曾颖：官员怎么会质问记者“替谁说话”<br />
2009-06-18 08:18:59　来源: 新京报(北京)　跟贴 408 条 手机看新闻 <br />
“你是准备替党说话，还是准备替老百姓说话？”郑州市规划局副局长逯军的问话反映出一个让人后背发凉的逻辑―――在某些人眼中，通过正规渠道进行的正常采访，成了“闲事”，而党和群众的关系，也被他直接地用一种对立语式并列起来。<br />
<br />
作者:曾颖&nbsp;&nbsp;作家<br />
<br />
河南郑州市须水镇西岗村原本被划拨为建设经济适用房的土地上，竟然被开发商建起了12幢联体别墅和两幢楼中楼。郑州市规划局副局长逯军面对记者的采访，在抱怨“你们广播电台管这闲事干什么？”之后，向记者问了这样一个问题：“你是准备替党说话，还是准备替老百姓说话？”（6月17日中国广播网）<br />
<br />
这位副局长不多的几句话，却生动有力地反映出一个让人后背发凉的逻辑―――在某些人眼中，通过正规渠道进行的正常采访，成了“闲事”，而党和群众的关系，也被他直接地用一种对立语式并列起来。无论在主流话语还是民间话语体系里，这位副局长的话都很犯忌。在网上网下引起强烈的批评，也就是意料之中的事。<br />
<br />
刚看这则新闻时，我有点不相信这句话是出自于一个副局级干部之口，作为一个受党培养多年的干部，他应该不会忘记“党和政府的宗旨是为人民服务，党和人民的利益从根本上讲是一致的”这一基本原则。这应该是公务员们长期记在心里、念在口里、落实在行动上的。况且，他所面对的是媒体，他应该知道说出这句话的后果。因而，我更愿意相信这位副局长是在情急之下的口不择言。<br />
<br />
但证据并不支持我的这种想法，因为他在说这句话时，前后逻辑是非常明晰的―――当得知记者是来质疑建设经济适用房的土地上被开发商建起了12幢联体别墅和两幢楼中楼这件事时，第一个本能反应就是“管闲事”，而这闲事，就是那些修在本应建设经济适用房的土地上，能赚一个亿的不公开出售的别墅。这背后究竟有什么样的秘密，没有谁能说得清楚。当然，也许在他看来，听招呼，守规矩的记者才是好记者。但遗憾的是，他所遇到的记者，并不如他所愿。<br />
<br />
当然，现在是法制社会，我们不能凭几句话，就判定谁的对错与赏罚。在没有新的调查结果之前，还不能轻易断言逯军副局长这段失态的言语背后，究竟有什么样不得已的苦衷和利害关系。希望他的话不会像周久耕先生处罚楼盘降价的言论背后，有不能不说的故事。但他的话，至少反映出他在世界观和语言表达的修炼上，还需要多多历炼自己。别的不说，单有一个基本常识需要他好好补习补习―――在经适房土地上盖别墅事件中，开发商和另外一些不便说出名字的人，肯定是最直接利益链中重要的一环，而曝光此事，一定会对他们的利益有威胁。副局长那句话的真实逻辑关系应该是：“你准备替开发商（和潜在利益获得者）说话，还是替百姓说话”？<br />
<br />
副局长并没说出其中真实的关系，而将党和百姓这两个概念对立起来，所暴露的，就不仅仅是个逻辑学的问题了。（原题：官员质问记者“替谁说话”，如此心态堪忧） (本文来源：新京报 )</div>

							
							
							
							
