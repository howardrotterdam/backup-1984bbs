													<h2>副局长质问记者：是准备替党说话，还是准备替老百姓说话？</h2>
						
						
																											<div id="postmessage_125130" class="t_msgfont">来源：<a href="http://www.china.com.cn/news/env/2009-06/17/content_17962675.htm" target="_blank">http://www.china.com.cn/news/env/2009-06/17/content_17962675.htm</a><br />
<br />
据中国之声《新闻纵横》报道，河南郑州市须水镇西岗村原本被划拨为建设经济适用房的土地上，竟然被开发商建起了12幢连体别墅和两幢楼中楼。日前，记者赶赴郑州进行了深入调查采访。<br />
<br />
宋先生是须水镇西岗村村民，2004年，郑州市政府将他们村的41.761亩土地划拨给了河南天荣置业有限公司建设经济适用房。在宋先生提供的郑州市城市规划局发放的建设用地规划许可证上，“建设内容”一项，也明确注明为“经济适用房”。但是几年过去了，开发商河南天荣置业公司却在这块土地上建起了12幢连体别墅和两幢楼中楼。宋先生说：“楼中楼是200多平方，别墅是300多平方，房已经完工了，销售也不是明着销售的，是暗箱操作。价钱他在网上按二手房销售的是4000多一平方。他这么多房子能卖一个亿，盖成经济适用房能错一半。”<br />
<br />
郑州市规划局面对记者采访摆起“空城计”<br />
<br />
宋先生已经向有关部门反映了这一情况，他说许多部门都很热情，郑州市规划局态度却极为冷淡。于是，记者在郑州市规划局信访接待日这天，拿着他们出具给宋先生的信访意见书，来到了规划局。在信访接待办公室门口的墙上，记者看到醒目的红色的牌子上写着，当天的值班领导是副局长李成祥，但是工作人员却说他一天都不在。<br />
<br />
工作人员：虽然是信访接待日，但是督导组，领导要求他们去，上午他们去督导组了。<br />
<br />
记者：那这个牌子不应该在这里挂了吧。<br />
<br />
工作人员：那怎么不应该在这里挂了？有什么事你向新闻中心提吧，好不好。信访不是对外的，我们局里有规定，统一对外接受采访是新闻中心，好不好。<br />
<br />
副局长质问记者:是准备替党说话，还是准备替老百姓说话？<br />
<br />
按照这位工作人员提供的电话，记者拨通了新闻中心陈主任的电话，但是她告诉记者，新闻中心没有一个人在家值班，全部出去了。几经波折，记者来到了主管信访工作的副局长逯军的办公室，将他们出具的信访处理意见书递了过去。一看到意见书上宋先生的名字，逯军就要求检查记者的采访设备，在拔掉了采访机话筒之后，他的第一句话居然是：“你们广播电台管这闲事干什么？”<br />
当记者要求他对于他们出具的信访处理意见进行解释时，这位副局长却向记者问了这样一个问题，他说：“<strong>你是准备替党说话，还是准备替老百姓说话？</strong>”这话让记者难以理解，众所周知，新闻媒体是党和政府的喉舌，而党和政府的宗旨是为人民服务，党和人民的利益从根本上讲是一致的，可为什么在逯军副局长眼里，党和百姓却成了对立的双方？记者要求他对这句话做出进一步的解释，这位副局长说：“这个事我不清楚。我也是第一次见到你，也是第一次见到这个事。等我搞清楚了以后我可以回复你。这件事我可以交有关部门来处理。”<br />
<br />
逯军副局长拿出笔来，居然在已经加盖了郑州市规划局信访专用章的处理意见书上又批示：请信访处办理。<br />
<br />
经适房管理中心:再不落实就要叫天荣公司退出<br />
<br />
在规划局，没有得到对于事件的任何解释，于是记者来到了郑州市经济适用房管理中心。严家新副主任态度非常积极，而且他的办公桌上就摆放着宋先生的举报材料。他告诉记者，他们共下达给河南天荣置业公司12.9万平方米的经济适用房建设计划，目前还有3万平方米的计划没有落实，经济适用房管理中心接到举报前就已经多次询问天荣公司：“我们到那一问，这房子盖的什么房子，我们不知道。谁盖的？不知道。土地规划给谁了？那不知道。他不给你说这一块的事。他这个计划再不落实，要按退出机制，叫这个天荣公司退出，政府给的计划为了解决低收入家庭住房困难问题，委托他们建设，他们落实不了，我们找能落实的企业去做。”<br />
<br />
宋先生说，划拨土地村民们要牺牲自己的利益，建设经济适用房，为住房困难群众做贡献他们没有意见，但是现在事与愿违，他们一定要把这件事追查到底：“政府这惠民政策城市市民没有得到房，没有得到实惠，开发商借机发财，这损失是国家的，也是老百姓的，我们村民坚决一追到底，把这政策落实给郑州市的无房家庭，不达到这个目的我们一天也不会停。”(任磊萍 何岩）</div>

							
							
							
							
