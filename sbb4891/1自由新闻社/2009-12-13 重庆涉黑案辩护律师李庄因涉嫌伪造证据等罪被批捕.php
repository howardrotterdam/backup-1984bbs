													<h2>重庆打黑曝律师造假事件 近20人被捕</h2>
						
						
																											<div id="postmessage_372804" class="t_msgfont">来源: 中国青年报(北京)　跟贴 1164 条 手机看新闻 <br />
<br />
&nbsp; &nbsp; 核心提示：12日，北京康达律师事务所律师李庄因涉嫌伪造证据、妨害作证罪被捕。涉黑“老大”检举称，李庄要求自己出庭只说3个字&quot;不知道&quot;。据悉，李庄刚到重庆就称要快速组建一支“跨区域打捞队”，并向京城同行发出信息：“够黑，人傻，钱多，速来!”<br />
<br />
&nbsp; &nbsp; 中国青年报12月14日报道2009年6月3日凌晨，龚刚模、樊奇杭黑社会性质组织为争夺“龙头”地位、扩展势力范围，在重庆江北区爱丁堡小区制造了一场血腥的黑道杀戮。枪声过后，警方锁定7条线索、发现7个黑恶团伙牵涉其中，迅即调集大批警力全力侦破。<br />
<br />
&nbsp; &nbsp; 经过缜密侦查，警方共抓获龚刚模、樊奇杭黑社会性质犯罪组织涉案人员125人，破获刑事案件25起，缴获各类枪支16支、子弹557发、手榴弹2枚、弓弩1把和管制刀具17件，查扣、冻结现金150.98万元、银行涉案资金46.44万元、房产13处及总值数亿元的车辆等涉案资产。<br />
<br />
&nbsp; &nbsp; 龚刚模等犯罪嫌疑人被抓获后，仍对“组织”抱有希望，用对抗、抵触、沉默等应付审讯。出人意料的是，在人民法院拟订开庭审理的日子里，龚刚模为争取立功，主动向警方检举了其辩护律师李庄、马晓军等人教唆其伪造证据、减轻罪责的犯罪事实。<br />
<br />
&nbsp; &nbsp; 12月13日，“律师造假门”始作俑者李庄被检察机关批准逮捕。一起国内罕见的涉黑案件“律师造假门”被急速曝光。<br />
<br />
&nbsp; &nbsp; 涉黑“老大”按响报警铃：我不想再与贪婪律师共同造假<br />
<br />
&nbsp; &nbsp; 11月下旬，重庆打黑除恶的一系列案件相继进入司法审判程序。与大多数市民一样，参与打黑除恶的民警们也在劳累之余密切关注着各项案件的审理宣判。<br />
<br />
&nbsp; &nbsp; 然而，此时在江北区，一个异常情况引起了民警的注意――涉黑“老大”龚刚模自从与他的辩护律师李庄等人会见后，情绪反常，心事重重，沉默寡言，尤其是12月4日与李庄第三次会面后，在看守所里一坐就是半天，不说一句话，茶饭不思。<br />
<br />
&nbsp; &nbsp; 为弄明原因，管教民警多次与其谈心，希望他摆正心态，调整情绪，正确对待即将开始的审判。<br />
<br />
&nbsp; &nbsp; 几经辗转反侧，龚刚模终于忍受不住内心的煎熬，按响了报警铃：“我有重要的情况要说!”<br />
<br />
&nbsp; &nbsp; 面对专案民警，龚刚模发泄般吐露几天来煎熬着他的秘密：他妻子从北京请来了康达律师事务所律师李庄、马晓军。在与李、马二人的前后3次会面中，李庄向他面授了五招“翻身秘术”：第一招是帮助他与其妻串证，变“黑老大”嫌疑为“受害者”、“慈善人士”;第二招是必须对法庭谎称被刑讯逼供，否认以前口供;第三招是向法庭提供虚假供述予以翻供;第四招是通报同案其他被告人对龚犯罪行为的供述;第五招是让他在开庭时以伤情鉴定为由，配合其扰乱庭审秩序，迫使法院休庭，拖延庭审。<br />
<br />
&nbsp; &nbsp; 李庄还例举了他在其他省市“捞人”方面的一些“成功案例”。<br />
<br />
&nbsp; &nbsp; “李庄教你这样做不是为你减轻罪行吗?你怎么会检举他?”专案民警问。<br />
<br />
&nbsp; &nbsp; 龚刚模说自己想了很久，“凭良心说，李庄为我出的这些主意就像一把双刃剑，可能是对我有好处，但他给我打官司就是为了钱，最终处理结果还得落在我身上。官司是否打得赢，他都出名了，如果被查出我作了伪证，倒霉的还是我自己!”<br />
<br />
&nbsp; &nbsp; 龚刚模还对民警说出了他的另外一个担心：在与律师李庄的3次会面中，李庄先后叫他签了七八张白条和委托书，“如果李庄在这些有我签名的白条和委托书上乱填些东西，那我不成冤大头了吗?假如他利用这个把我的财产弄走，我找谁说去?”<br />
<br />
&nbsp; &nbsp; 其实，在龚刚模按响报警铃之前，律师李庄等人违法操作、妨碍正常司法的行为已引起相关部门的警觉，巡查民警多次批评和警告，李庄仍置若罔闻，看守所依法作了详细记载。<br />
<br />
&nbsp; &nbsp; 随着调查深入，一起国内罕见的“律师造假门”被层层剥开。<br />
<br />
&nbsp; &nbsp; 律师李庄的确能“装”，一手“捞人”一手“捞钱”<br />
<br />
&nbsp; &nbsp; 李庄，48岁，混迹律师界十余年，其所在的康达律师事务所在京城也颇有“背景”。注重“身价”的李庄此次肯来重庆打涉黑官司，除受龚刚模的生意伙伴相邀答应来“捞人”，其实更重在“捞钱”。<br />
<br />
&nbsp; &nbsp; 10月下旬，龚刚模亲友为“搭救老大”，往律师事务所的账上先拨付20万元，但这远远填不满李庄的胃口。<br />
<br />
&nbsp; &nbsp; 他以要在北京召开“专家分析会”为由，又向对方索要了30万元。到重庆后，他对龚刚模的亲属龚云飞说：“这个案子有点复杂，律师费偏低哟，而且我还要去‘打通’。”迫于无奈，龚云飞又托人再次给李庄的账户“装”了100万元。<br />
<br />
&nbsp; &nbsp; “捞钱”到账，李庄等人便着手实施“捞人”。在三次与龚刚模会见、面授机宜后，12月3日,李庄来到市第一中级法院，提出了被告人龚刚模在侦查过程中被刑讯逼供、无法正常会见当事人，以及被告人关押地点违法等一系列杜撰的问题。<br />
<br />
&nbsp; &nbsp; 随后，李庄授意龚刚模的弟弟龚刚华，邀了当地保利夜总会主要负责人李某，在高新区奥体中心附近一茶楼大厅见面。按照李庄的指点，龚刚华要求李某在龚刚模审判阶段将保利夜总会关闭。对此，李某说个人无法做主，返回保利夜总会约部门负责人汪某和陈某再次来到茶楼。3人在龚刚华驾驶的轿车内，协商关闭保利夜总会事宜，要求让一些已经被警方传唤过的“污点证人”赶紧逃跑，并把龚刚模夜总会老板的身份“转嫁”出去。<br />
<br />
&nbsp; &nbsp; 龚刚华对汪某说：“龚刚模将夜总会股权转让给了在逃的唐某，即使警察找到了唐某也要说他才是保利夜总会真正的老板。”<br />
<br />
&nbsp; &nbsp; 但是经过司法调查，李庄的种种造假设计经不起推敲，其“刑讯逼供”和“无法正常会见”等种种说法不攻自破。<br />
<br />
&nbsp; &nbsp; 为设置更多障碍，李庄不断炮制出新的质疑，如检察机关移送证据不足、龚的交待笔录出现多份雷同等。<br />
<br />
&nbsp; &nbsp; “检察机关移送的主要证据复印件，与拟在法庭举示的证据内容基本一致，符合《人民检察院刑事诉讼规则》关于移送主要证据范围的规定;而笔录雷同系多位侦查员在不同时间对龚进行讯问，其内容不可避免会出现相同或相似。”该案件调查组工作人员说。<br />
<br />
&nbsp; &nbsp; 李庄要求龚刚模出庭只说三个字：不知道<br />
<br />
&nbsp; &nbsp; 李庄、马晓军等律师教唆龚刚模翻供、串证等问题一露端倪，立即引起了重庆打黑领导小组的高度重视，迅速组织公、检、法、司人员成立联合调查组。<br />
<br />
&nbsp; &nbsp; 调查组现已查明：11月24日，李庄在首次会见龚刚模的过程中，即向龚刚模宣读同案多名犯罪嫌疑人的笔录材料，特别是宣读了同案另一主要犯罪嫌疑人樊奇杭的多份交代笔录，同时还把同案重要嫌疑人的在逃信息告诉了龚刚模。<br />
<br />
&nbsp; &nbsp; 11月26日，李庄在第二次会见中对龚说：“从你的材料中看得出来，你肯定被诱供和刑讯逼供了。法庭上问你是否被刑讯逼供时，你要大声承认，还要把刑讯逼供的过程夸张地演示出来，以刑讯逼供为由否认原来在侦查阶段所作的口供。”同时，李庄还编造了一大堆“细节”，要求龚刚模在法庭说：“以前的口供全是瞎编的，因为我被公安吊了八天八夜，被打得大小便失禁。”<br />
<br />
&nbsp; &nbsp; 李庄用威胁口吻告诉龚刚模：“如果依照刑讯逼供所说的笔录就得枪毙你……翻供你要有道理，有理由。”<br />
<br />
&nbsp; &nbsp; 事后，李庄对龚云飞说：“我叫龚刚模说被刑讯逼供了，到时在法庭上咬定民警打了他、吊了他，这样才能推翻以前对龚刚模不利的证据。”<br />
<br />
&nbsp; &nbsp; 李庄还对龚刚模提出：“在开庭时，我会要求休庭，对因刑讯逼供造成的伤情进行司法鉴定。如果法庭不同意，我就会提出不再担任你的律师，那么法庭就会让你在三天之内找新的律师，如果你找不到，法庭会给你指定律师。你知道怎么做吗?你坚决不要接受法院指定的律师，只要我给你辩护，法庭就开不了庭。我在辽阳办理的一起案件中，就用这招拖了法院一年半!”<br />
<br />
&nbsp; &nbsp; 12月4日，李庄在第三次会见过程中，向龚刚模明确表示已联系多名证人配合伪证，并教唆龚刚模配合编造事实来印证，企图回避案件事实。<br />
<br />
&nbsp; &nbsp; 李庄对龚刚模说：“已经安排在开庭的时候由你老婆到庭上给你作证，证明你不是黑社会。”李庄教唆他说，“你老婆到时会说樊奇杭、李明航找你借钱实际上是敲诈，主要是这些人得罪不起”，以达到弱化龚刚模在黑社会组织中的领导地位和作用。“从你的角度上去说。比如说，樊奇杭、李明航打电话找你要钱，你回去找你老婆借钱啊!你老婆说，不借给他。你说，不借不行啊，咱们惹不起他啊，咱们怕他!”<br />
<br />
&nbsp; &nbsp; 同时，李庄在会见过程中还教唆龚刚模不要承认贩毒、贩枪、开设赌场等违法犯罪行为，并说：“对开赌场、贩毒、贩枪这些你是不知道的，当法庭上问这些问题时，你就说没有，真不知道，就是不知道，就这三个字完了，别的不要多说。”<br />
<br />
&nbsp; &nbsp; 李庄甚至要求龚云飞在重庆市大足县林业局虚开被告人公司在大足县植树造林、造福一方的证明，以表现龚刚模对社会具有一定贡献，想尽一切办法为其脱罪。<br />
<br />
&nbsp; &nbsp; 据犯罪嫌疑人龚云飞交待：“我当时就明白了，立即按他意思去开了证明。”<br />
<br />
&nbsp; &nbsp; 李庄打“广告”：这里“够黑，人傻，钱多，速来”<br />
<br />
&nbsp; &nbsp; 经调查，李庄一到重庆，就炫耀自己“上面有人”，多次说“你知道我的背景是什么吗”、“我的头儿是谁你知道吗”，并滔滔不绝地讲述自己多次“捞人”的“成功案例”。<br />
<br />
&nbsp; &nbsp; 李庄告诉龚刚模的亲友，自己要快速组建一支“跨区域打捞队”，为此，他已经在北京、成都、重庆等地聚集了一帮“高人”。<br />
<br />
&nbsp; &nbsp; 龚刚模的亲友“捞人”心切，再加上李庄的多番“演说”，几天之内就总共支付了245万元给“跨区域打捞队”。李庄代表“打捞队”要龚刚模的亲友承诺：若要龚刚模不判死刑，还要两三千万元，事成之后兑现。<br />
<br />
&nbsp; &nbsp; 但花费了巨资的“龚刚模亲友团”怎么也不会想到，他们的大投入在李庄眼中只不过如同“烹羊宰牛”。欣喜之余，李庄向京城同行发出信息：“够黑，人傻，钱多，速来!”<br />
<br />
&nbsp; &nbsp; 一位不愿意透露姓名的重庆政法干部告诉中国青年报记者，重庆打黑除恶一系列案件进入司法程序后，“到重庆代理涉黑诉讼”一时成律师界热门。许多北京律师如赶场般云集重庆，寻找开展“业务”和施行“潜规则”的机会。<br />
<br />
&nbsp; &nbsp; 该官员分析说，“李庄现象”泛滥的背后，是“潜规则”还有其存在与蔓延的空间，一方面，我国《律师法》相对超前而其他法律相对滞后。另一方面，律师行使“潜规则”是司法腐败的支撑点之一，并是一些企业家涉嫌犯罪后“荣请”的对象，为保命或减轻刑罚往往不惜数百万、上千万地投入“打捞”。更可悲的是，一些律师或律师事务所为了获得高额回报，竟然还发明了“内部潜规则”：代理律师一旦“事成”，律师事务所就将其除名，过一段时间再聘回“重新上岗”;另一种瞒天过海的招数是随机改变代理律师，即由一个或一批律师充当“先头部队”，施行“潜规则”拿到钱后再更换成别的律师出庭应诉。<br />
<br />
&nbsp; &nbsp; 李庄等近20人被捕，律师何以知法犯法<br />
<br />
&nbsp; &nbsp; 12月12日，已有警觉、潜回北京的李庄给重庆法院的一位领导发来一条短信：“经组织决定，我们康达律师事务所两名律师全部从龚案撤出，不再担任辩护人，请转告有关方面。”<br />
<br />
&nbsp; &nbsp; 但当晚，李庄就被重庆市公安局依法传唤。12月13日，李庄被检察机关批准逮捕。<br />
<br />
&nbsp; &nbsp; 截至记者发稿时，此案中因涉嫌伪证罪被司法机关刑拘、逮捕和缉捕的已近20人。此案仍在继续深入调查。<br />
<br />
&nbsp; &nbsp; 我国《刑法》第306条规定：在刑事诉讼中，辩护、诉讼代理人毁灭、伪造证据，帮助当事人毁灭、伪造证据，威胁、利诱证人违背事实改变证言或者作伪证的，处三年以下有期徒刑或者拘役;情节严重的，处三年以上七年以下有期徒刑。<br />
<br />
&nbsp; &nbsp; “应该说，一般律师都应该熟悉《刑法》第306条，李庄作为资深律师，更应烂熟于心。是什么让此案的数名律师对这柄高悬之剑熟视无睹铤而走险?除了金钱诱惑外，想必还有一个更重要的原因，那就是让李庄之流有恃无恐的所谓‘关系背景’。”一位重庆政法系统官员对中国青年报记者说。<br />
<br />
&nbsp; &nbsp; 最新调查表明，在这起“律师造假门”中，除了李庄所仗恃的个人背景关系外，重庆的两名律师也向龚刚模亲友索取了95万元用于“活动”，成为“造假门”涉案者，并企图以金钱开道，利诱政法部门的个别干警加入“造假生产链条”。目前，其中一名重庆涉案律师吴家友已被警方捕获。<br />
<br />
&nbsp; &nbsp; 据资料，在刑事案件中，律师胜诉的比例仅有5%，也就是95%是败诉。<br />
<br />
&nbsp; &nbsp; “面对当事人的巨大诉讼投入，有多少律师在说明败诉原因之余会对当事人说‘对不起’?当事人有苦难言，实际上造成了‘第二次伤害’。律师的尴尬作为和滥用‘潜规则’，所造成的灾难全由国家和民众来承受，公信力弱化由政法机关来承受，从众心理、潜规则冲击着党和政府的形象，让党和政府来埋单!”这位重庆政法系统官员最后说。<br />
