南方报业网-南方都市报　 　 我要评论(710) 导读：整部影片以九城和网易争夺魔兽世界代理权事件为基本框架，把对游戏内容的审批和修改，两家公司的明争暗斗都嘲讽了一遍，另外还穿插了大量的2009年网络热点事件，比如钓鱼执法，七十码等。以网络游戏《魔兽世界》为蓝本，网友、玩家出演，调侃电击治网瘾的杨永信<br />
<br />
过去几天，一部用魔兽世界游戏场景制作的“山寨电影”《网瘾战争》在网上爆红，让无数网友为之心折为之感动，四处推荐。杨永信在这部电影里被代表正义的魔兽玩家击败。另外，网友们还挖出了杨当年曾经申请过专利的称百分之百有效的治疗网瘾药物，时间还在他用电击手法之前。<br />
<br />
影片里钓鱼执法、70码都有<br />
<br />
电影全称《看你妹之网瘾战争》，基本上全用的是《魔兽世界》里的游戏场景截取编辑而成。作者是魔兽世界的著名玩家“性感玉米”，制作团队叫“爱老虎油”，参与配音的都是魔兽世界中的著名玩家。“看你妹”本是游戏中网友相互调侃之语，在片中成为主人公的名字。电影作者像所有魔兽玩家一样玩不到资料片《巫妖王之怒》，进而调侃杨永信。<br />
<br />
片中对迟迟不开《巫妖王之怒》愤怒不已：“房价高！足球臭！还不让老子玩魔兽！等了这么久了，连阿娇都复出了！巫妖王还没给我开？”<br />
<br />
整部影片以九城和网易争夺魔兽世界代理权事件为基本框架，把对游戏内容的审批和修改，两家公司的明争暗斗都嘲讽了一遍，另外还穿插了大量的2009年网络热点事件，比如钓鱼执法，七十码等。<br />
<br />
片中反角之一“杨永新”明显影射杨永信，他的特技是“核谐电击”，成为阻止新版魔兽世界上市的主要阻力。主角对着杨声讨说：“我们是玩着游戏长大的一代人。这么多年来，人变了，游戏也变了，可我们对游戏的喜爱没有变。我们玩家群体在这个社会中的弱势地位没有变。当我们累了一天，打开那扇月租2000元的房门却面对的是如此畸形的一个游戏时，心里只有无奈。”<br />
<br />
性感玉米：我就是觉得憋屈<br />
<br />
性感玉米接受了本报记者的采访。他说，制作这部电影的主要原因就是2009年一年来，魔兽世界的玩家因为不能在自己国家的服务器上玩这个游戏，就算玩到了也是被修改后的版本，感到无比憋屈：“因为国服魔兽已经被修改得我自己不能接受，所以我去了台湾地区的服务器玩，但刚去的时候，被当地玩家所歧视”，“我作为一个游戏玩家，本来是这个社会上最无害的一群人，但现在却无法在自己的服务器与朋友一起玩游戏。”另外，他也十分反感舆论对网游玩家的妖魔化，于是就把杨永信当成了一个代表人物，在影片里进行调侃。<br />
<br />
电影时长达一个小时有余，制作时间超过三个月。先是由性感玉米写好剧本，请网友在游戏中进行表演，截取视频。然后再通过网络请网友配音。但这部作品除了他家的电费之外，没花一分钱。<br />
<br />
作品赢得网友称赞，甚至是不玩魔兽的网友也在推荐。最后一段主人公号召所有的普通玩家举起手，共同反抗对手的时候，有网友哭了。有网友评论说，“当看着《网瘾战争》中那些小人举起自己的手，我感受到的是如海潮般的共鸣。再小的力量也是一种力量，再小的声音也是一种声音，这次，我不想再沉默。”而视频下面的跟帖中，更是有大量的网友在“举手”。本报记者谭人玮<br />
