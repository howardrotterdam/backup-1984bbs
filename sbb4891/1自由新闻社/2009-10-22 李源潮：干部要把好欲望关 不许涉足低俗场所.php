																											<div id="postmessage_295589" class="t_msgfont">李源潮：干部要把好欲望关 不许涉足低俗场所<br />
<br />
2009年10月22日13:25<br />
<br />
<img src="http://www.jschina.com.cn/gb/jschina/news/zt200601/node17869/node17873/node17980/images/00253914.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　中组部部长李源潮近日指出，党员干部要抗得住诱惑，把好欲望关。必须明确一条约束，党员领导干部不许涉足低俗场所，不许找“三陪”小姐陪酒陪唱，否则干部考核时就要在日常品行方面记上污点。<br />
<br />
　　李源潮是在浦东、井冈山、延安干部学院2009年秋季开学典礼上说这番话的。新一期中央党校主办的《学习时报》，以《共产党的干部必须清正廉洁》为题，刊登了这一讲话的删节版。<br />
<br />
　　李源潮指出，党的干部要洁身自好。廉与洁不可分割。古人说：“不受曰廉，不污曰洁。”陷入腐败深渊的领导干部，多数是从不能洁身自好开始的。现在有些干部认为小节无害，贪图安逸，情趣庸俗，出入低俗场所甚至色情场所，沉溺于灯红酒绿、吃喝玩乐，损害党的形象、败坏党风民风。<br />
<br />
　　胡锦涛总书记指出，加强领导干部作风建设，必须把倡导良好的生活作风和健康的生活情趣作为一个重要方面。我们要按照“讲党性、重品行、作表率”的要求，自觉培养健康生活情趣，保持高尚精神追求。<br />
<br />
　　李源潮要求党员干部要抗得住诱惑，把好欲望关。他说，现在领导干部面临的诱惑很多，如果放纵私欲，就会迷失人生方向，成为权钱色的俘虏。从最近发生的一些贪官绯闻中发现，一些干部腐化堕落是从到低俗场所找“三陪”开始的。他呼吁领导干部要加强品行修养，节制不良欲望，保持艰苦奋斗作风，自觉做到不追求物质享受，不沉湎感官刺激。<br />
<br />
　　第二，李源潮要求党员干部要择善而交，把好交友关。他说，现在社会上有一些居心不良的人，挖空心思与领导干部套近乎，把掌有实权的干部当作“资源”来经营，把表现优秀的年轻干部当作“潜力股”来投资。领导干部一定要对那些怀着个人目的来拉拉扯扯的人保持高度警惕，严把“社交圈”，更不能为了贪图享乐而去“傍大款”。要多同普通群众交朋友，多同基层干部交朋友，多同先进模范交朋友，多同专家学者交朋友，见贤思齐，见不贤而自省。<br />
<br />
　　第三，要培养健康情趣，把好兴趣关。领导干部应该有有益身心健康的生活情趣，但生活情趣不是无关紧要的小事，弄得不好，很容易成为别有用心之人公关的突破口。赖昌星腐蚀领导干部的一个重要手段，就是研究干部的兴趣爱好，投其所好、拉人下水。高尔基说过，“人的价值表现在心灵上”。领导干部选择自己的爱好，一定要“爱”之得当、“好”之有道，使其有利于陶冶情操、完善人格，提升人生价值。<br />
<br />
