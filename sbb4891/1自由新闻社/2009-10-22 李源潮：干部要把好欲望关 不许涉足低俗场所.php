　　中共中央政治局委员、中央书记处书记、中组部部长李源潮近日指出，党员干部要抗得住诱惑，把好欲望关。必须明确一条约束，党员领导干部不许涉足低俗场所，不许找“三陪”小姐陪酒陪唱，否则干部考核时就要在日常品行方面记上污点。<br />
