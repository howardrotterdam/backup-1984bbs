													<h2>《晚清70年》作者唐德刚在旧金山过世</h2>
						
						
																											<div id="postmessage_304565" class="t_msgfont">台湾《联合报》报道，著名美籍华人学者、历史学家、传记文学家、红学家唐德刚先生，已于周一(26日)晚间在美国旧金山家中因肾衰竭过世，享年89岁。 <br />
<br />
唐德刚1920年生于安徽合肥，曾经参军并做过中学教员。1939年秋考入重庆国立中央大学(1949年更名南京大学)历史学系，1948年赴美留学，获哥伦比亚大学博士学位后，留校任教。1972年受聘为纽约市立大学教授，后兼任系主任12年。 <br />
<br />
身为中国近代史大家，唐德刚也是华裔史学家中口述史的主要推动人物。他与当时政要如顾维钧、李宗仁、陈立夫、张学良等人多有接触，和当时人在纽约的胡适则为忘年之交。曾著有《李宗仁回忆录》、《胡适口述自传》、《顾维钧回忆录》、《晚清七十年》、《张学良口述历史》等。 <br />
<br />
<br />
他的散文可读性颇高，旅美学人夏志清教授在《胡适杂忆》序中说，唐德刚先生“应公认是当代中国别树一帜的散文家。他倒没有走胡适的老路，写一清如水的纯白话。德刚古文根柢深厚，加上天性诙谐，写起文章来，口无遮拦，气势极盛，读起来真是妙趣横生”。 <br />
<br />
他还曾参与发起在全球征集一亿人签名要求日本偿付战争赔款的运动。 <br />
<br />
其夫人吴昭文，是中国国民党元老吴开先之女。吴昭文表示，唐德刚生前饱受肾病折磨，后来决定不再洗肾，在旧金山家中安详辞世。</div>

							
							
							
							
