													<h2>贴旧文一篇    聊表哀思</h2>
						
						
																											<div id="postmessage_305870" class="t_msgfont">“先看德刚，后看胡适”<br />
── 读《胡适口述自传》感言<br />
<br />
&nbsp; &nbsp; 日前得见《胡适口述自传》一书，最初不过是随便翻翻，没料到一翻便不能释卷。并非传记本身有何种了不得的吸引力，诚如记录兼翻译者唐德刚先生所言：它的内容的根本没有什么新鲜材料；它的观点表明胡适晚年思想与中青年时期相比几乎没有什么变化──往好里说是前后一致，往坏里说是没有长进。让我一口气读下去的倒是各章正文后洋洋洒洒的若干条注释。注者自然是唐德刚先生。最奇特的是有的注释一条即长达数千言，注释的整个篇幅占到了全书的约三分之一强，简直就是一篇篇独立的小文章了。注释内容除了一般性的提供引文出处，交代背景材料，澄清某些事实外，更多的是由胡适的所述生发开去，或辩驳、或评判、或质疑，或联系当今世界现状深化论题，其思路不法常可，纵横捭阖，迭有新见，行文上则议论风发，嬉笑讥嘲，机锋毕露，甚至达到了”口诛笔伐“的程度。读者痛快之余，不免也有点为“恂恂儒雅”的白面书生胡适之抱屈，觉得注释者知人论事透辟是透辟，但未免也喧宾夺主、太不留情面了些。<br />
&nbsp; &nbsp; 其实，作为“后辈”“后学”、胡适晚年客居纽约时之忘年交的唐德刚，对一代“文坛祭酒”是极推重的，否则他就不会作出“胡适之先生是现代中国最了不起的大学者和思想家，他对我们这一代，乃至今后若干代的影响是无法估计的”这样高的评价了。只不过这种推重跟常人在权威脚下诚惶诚恐的崇仰有所不同。唐先生是40年代末留学欧美的那一代中国知识分子，生活和学术环境决定了他们身上传统士大夫气的淡薄，他们对老师抑或权威都无一例外抱有怀疑态度，从不一味迷信。唐德刚的注释，正是这种怀疑精神的具体表现，他的推重也是怀疑以后的肯定。<br />
&nbsp; &nbsp; 《自传》系胡适英文口述自传全稿的卷上（下卷由于胡适60年代初不慎贪酒身亡而终未做出），大体上包括了他早中期在新文学、国学及其他学术领域内的主要活动与建树，林林总总，涉猎甚广。注释者的品评阐发更是中外古今、文史经哲无所不谈，其中隽语高论一时难以尽述，此处只能略举一二：<br />
&nbsp; &nbsp; 其一，对“五・四”前后由搞“新”文学、“新”文化而一头钻入旧书堆大搞“国故整理”的胡适，左中右翼学人都无一例外地加以责难，胡适的自我解释是“再造文明”，而整理国故是“再造”的第一步。唐德刚对此的评述是：当年启蒙大师们的专业多为“输入学理”（或传抄革命标语，或引进欧美“教科书”），然国内并无足够条件供归国的洋博士们对西洋玩艺儿“继续进修”，于是这帮人只有三条路可走：1.“实行学理”，组党干革命；2.人家“革命”，我来“建国”，如“独立评论派”；3.死守山林，作个纯学者，适之先生即属此类（编者按：恐怕只能说是一度如此。）──虽限于“地利”难于继续“输入学理”，但祖学所积甚厚，马首一掉，便整理那永远不可能整理清楚的“国故”去了。由此唐德刚进一步指出，一个“现代国家”的政治、经济、文化诸方面是相互连锁成为整体的，如果经济上还处于“发展中”，文化学术就不可能“已发展”甚至“超发展”，从这种角度看，毋宁说胡适倒是量体裁衣，搞了他所处的那个时代所能搞的“恰当学术”，即不中不西、土洋并举的“科学方法的批判的整理国故”。简言之，百年来的中国现代化运动是一体的，其他领域尚未现代化，学术现代化是不可能的，只能搞点过渡时代的学术。<br />
&nbsp; &nbsp; 其二，20年代中──30年代初，胡适因撰写禅宗史曾对有关佛教史料作了一番搜寻研究，得到了他自诩为“足以改写禅宗史”的重大发现，然这位“学界巨子”对宗教的议论却惊人的武断和无知。《自传》第十二章中专列了“揭穿认真作假的和尚道士”一节，大骂禅宗佛教的“胡说”、“诈骗”和“装腔作势”。唐先生对此讥评道：适之先生谈宗教的弱点恰是他“整理国故”弱点的反面，“整理国故”科学似不太够，而“整理佛教”科学又太多了点。笔者也曾听到过这样一桩轶事，40年代，某团体议请梁漱溟先生讲佛学，某君（此人修习禅宗已有大成）却微微一哂：“梁漱溟懂什么佛学？！”语出四座皆惊。从学问角度看，自不能说梁不懂佛学，然“学问”与“宗教”并非一码事，前者重在“学”、重在“识”，后者重在“信”、重在“悟”，没有“学佛”，无出世之心的人要谈“出世之学”，必然只能是隔靴搔痒。“所以一个学者，断不可以处理‘学问’的方法去处理‘宗教’，宗教是另有一番境界的。不论有神的耶教，无神的佛教，在这一境界里是殊途同归的，‘真’与‘假’，‘骗’与‘不骗’，俗人的心智而已，实在无关宏旨。”── 德刚先生如是说。又想起前些时某书评杂志上有篇读《弘一大师传》的文章，文未告诫读者“弘一法师走过的条路不值得效法”云云，当时不禁失笑，今见学富五车的胡博士尚且如是说，也就不以为怪了。<br />
&nbsp; &nbsp; 胡适的《自传》，唐德刚的注释，所涉甚广，如对近百年来中国留美教育史的看法，对世纪初“白话文运动”的肯定与否定，以及对“五・四”运动与新文化运动相互关系的探究等等，读者诸君如能细加品鉴，想必会有收益。唯注者以抬扛者身份出现，所抬之杠却不免有得有失，甚或有为抬杠而抬杠处（如第三章注十九）。人非圣贤，无须苛求，然而这抬杠的态度与方式，倒是有值得我们借鉴之处。<br />
&nbsp; &nbsp; 据书前的译后感述，唐德刚的老朋友朱文长教授曾信告他说，“我读胡适自传是先看德刚，后看胡适”，可见对唐先生注释的器重。一般来说，“喧宾夺主”乃为文大忌，不料唐先生宾主这样一倒，居然倒出了新鲜花活儿来。真所谓作文无定法，读书亦然。<br />
