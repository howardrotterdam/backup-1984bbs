																											<div id="postmessage_305067" class="t_msgfont">生论政的“晚清七十年”<br />
2006-10-09 03:42:21 　　来自: 九段 <br />
晚清七十年的评论&nbsp; &nbsp; <br />
<br />
　　唐德刚的“晚清七十年”名气很响，多次有朋友向我提起。这恐怕与此书在大陆难以出版有一点关系。至于唐德刚，我知道他是我同乡，仅此而已。之前也只读过他的一本小册子《袁氏当国》，感觉良好。于是我托朋友从香港带来了一套“七十年”。 <br />
　　 <br />
　　一口气读完，唐氏对中国近代七十年，其实不止七十年，历史娓娓道来，颇为可观。首卷中的议论虽然痛快淋漓，但也不无偏颇与意气。再有，唐氏在书中一再提及的“历史三峡”论，俺却不敢苟同。按照唐的看法，中国近代所有历史事件无非是历史三峡中的一段险滩而已，乃必经之路，而历史的结局也必然是某种“人类最高阶段”的先验结果。就很有些历史决定论的味道。若依此说，中国近现代所有历史人物的作为与努力皆为虚妄，中国在向现代化的自由民主道路必须经过这些阶段，恐怕这连唐老先生自己也未必相信。 <br />
　　 <br />
　　可以说，历史是充满了人的自由意志的，它并没有什么必然与先验。人类在文字尚未出现以前，历史是怎么样的呢？恐怕不会有什么历史三峡之类的东西出现。也正是历史的无常变化，人类才将这种变动归于神，历史是由神来操控的。这是另一个极端。 <br />
　　 <br />
　　唐先生是研究历史的，却不知道假若我们在看历史时，连个“如果”都不能提出，这历史还有什么意义呢！历史是充满了偶然的，是由无数个偶然的事件串连而成的。唐先生恐怕对政治也是了解不多或是不够的，所以才会有此说。也许，是由于近百年来中国的独特环境所造就的吧？清末有十二年的预备立宪期，孙先生也有“军政、训政、宪政”的阶段说。善意地猜度，唐先生认为中国尚不具备某种条件，如文化决定论的调子。或者这本就是唐先生在向孙先生致敬也未可知。不善意地，可能就有帮闲的嫌疑了。假若历史三峡果真存在，那么，人类历史的终极目标也就理所当然，在向这一终极目标迈进时，任何牺牲也就可以容忍，也就是必然和必要的了。这岂不是可怕得很？！ <br />
　　 <br />
　　到了今天这几“政”也换成了几个“阶段”了。呵呵，历史再次成了某些人手中的工具，用之即来挥之即去了，真是方便得很。没准，这又是另一种形式的“历史三峡”吧？</div>

							
							
							
							
