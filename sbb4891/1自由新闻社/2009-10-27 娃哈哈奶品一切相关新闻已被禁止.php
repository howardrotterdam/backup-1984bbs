本报记者将对夏先生的情况展开进一步的调查。 <br />
“事情已经闹到这个份上了，一定要弄个分晓出来，我现在唯一担心的是检测机构存私心，为了让分层产品得到公正公平的检测，我委托你们媒体帮我将产品送检”。唐强说。 <br />
目前，消费者唐强写好委托书，委托本报将分层的娃哈哈营养快线产品送往湖南省质监局和国家质监总局的权威检测机构检验，并给本报送来了一些出现分层的娃哈哈营养快线样品。他表示，一定要有个权威的检测结果，让娃哈哈营养快线对全国消费者有个交代。唐强还还准备联合全国各地其他娃哈哈营养快线问题产品的投诉者，在网络和社会上对娃哈哈营养快线进行声讨。 <br />
<br />
网友 <br />
“娃哈哈成‘娃泻泻’” <br />
本报“数名家长投诉小孩喝娃哈哈营养快线后患病”的报道一时间成为了各大新闻门户网站和网友们关注的热点，引发了一场关于娃哈哈营养快线质量问题的大讨论。凤凰网还以“娃哈哈营养快线‘致病’风波”为题推出了专题网页。 <br />
在上期报道中，面对《法制周报》记者的提问，“为什么产品在保质期内，同批次同型号的产品会出现分层、沉淀现象”。娃哈哈公司方面回复说，“哪个做食品的企业都不能保证食品百分之百的不存在问题”，“可能是流通领域操作不当，造成瓶盖松动，引发微生物进入，而造成产品变质。” <br />
&nbsp; &nbsp;网友对娃哈哈方面的这些说法并不买账。有网友在看到娃哈哈方面的解释后发表评论：”看到这样的解释，我实在惊恐，食品企业关系公民生命健康，就应该保证百分之百没质量问题，如果不保证，公民生命健康安全就成了儿戏，如此观点让人汗颜。”同时又有网友质疑“流通领域操作不当，造成瓶盖松动”的说法，“如果运输过程中瓶盖松动，那么，这个包装质量合格吗？” <br />
　　面对娃哈哈方面的解释，许多网友表示难以理解，同时，“娃哈哈”品牌也被网友恶戏称为“娃泻泻”。部分网民表示，企业相关部门就应该赶紧调查原因，给消费者道歉，赔偿消费者损失，这是才是一个个大企业集团应有的责任，应有的形象。 <br />
有网友戏称娃哈哈相关人员的解释确实很雷人，让人汗颜，“‘百分之百说’很雷人，‘哪个做食品的企业都不能保证食品百分之百的不存在问题’。看到这样的解释，我实在惊恐，食品企业关系关系公民生命健康，就应该保证百分之百没质量问题，如果不保证，公民生命健康安全不成了儿戏，如此观点让人汗颜；‘肯定说’很雷人。 <br />
而杭州娃哈哈集团有限公司售后服务部的方先生向记者表示，“产品在生产制造过程中肯定是不存在问题的，如果出现分层，可能是流通领域操作不当，造成瓶盖松动引发微生物进入，而造成产品变质。”网友们称，这个解释把自己的责任推卸的一干二净，然而，我们不仅要问，如果运输过程中瓶盖松动了，那么，这个包装质量合格吗?我看，这个‘肯定说’有点大言不惭，恬不知耻” 。 <br />
