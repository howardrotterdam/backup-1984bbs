 <br />
本人经过深入调查，于10月16日发表《数名家长投诉小孩喝营养快线后患病》一文后，引起了网络及社会各界的强烈反响，自去年“三鹿事件”后食品安全问题引起了人们的强烈关注。今天上午看到娃哈哈集团发出的一份毫不负责任的所谓声明，并采取各种手段企图掩盖真相，本人特发此篇后续报道，以正视听！ <br />
