&nbsp; &nbsp; 深圳的一位消费者反映：2008年10月11日的晚，他在街上买了两瓶菠萝味的娃哈哈的营养快线，当时喝了一瓶，可第二天却突然发现另外一瓶没有开封的里面有一个黑色的异物，赶快看生产日期“20081210&nbsp;&nbsp;前饮用”。当时就想莫非娃哈哈的营养快线也有假冒产品，立马在网上搜索发现这已不是营养快线的第一例事件了，后来向娃哈哈投诉等了3天终于来了人，经确认这的确是天天在电视上做广告的货真价实的娃哈哈营养快线，包装完整没有任何损伤。 <br />
&nbsp; &nbsp;&nbsp; &nbsp; 面对这样的一款还在有效期内的包装完好、尚未开瓶的产品，娃哈哈驻深圳的客户服务部门，不同的人却给了不同的说法：1、黑色物体是焦糖沉淀，是正常现象。2、黑色物体是在运输过程中因为碰撞导致进入了空气产生的霉变物。 <br />
该消费者对此表示质疑，这听起来好象有道理，可是焦糖沉淀为什么不是均匀的细小颗粒？为什么在包装上没有文字说明？“第二条理由完全是推卸责任。” <br />
&nbsp; &nbsp;一些消费者表示，希望娃哈哈集团是一个负责任的企业，希望他们能够真正的搞清楚这频频出现在密封好的瓶子里的黑色不祥之物，究竟是怎么进入这瓶子里的，到底对人体有没有害，以便保证我们消费者健康。 <br />
