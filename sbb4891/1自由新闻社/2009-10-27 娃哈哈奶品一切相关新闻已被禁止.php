&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;娃哈哈营养快线“致病”风波追踪 <br />
<br />
<br />
《法制周报》记者&nbsp;&nbsp;蒋格伟 <br />
<br />
10月16日，本报刊发《数名家长投诉小孩喝娃哈哈营养快线后患病》报道后，迅速引起了社会各方的强烈关注，引发了一场关于娃哈哈营养快线质量问题的热议，该报道引起了近百家网站转载。 <br />
报道发出后，本报新闻热线接到了全国各地的消费者关于娃哈哈营养快线质量问题投诉，许多媒体记者纷纷联系本报记者要求联动采访，并对娃哈哈营养快线的质量提出了质疑，还有英国《金融时报》等境外媒体联系本报表示对此事件表示关注。一些网友和记者还表示担忧：此次娃哈哈营养快线风波是否会引起下一个“三鹿”事件。 <br />
10月19日，娄底市娄星区工商局相关负责人接受《法制周报》记者采访时表示，工商局已责令出现问题的该批次产品（生产日期为2009年5月8日，350ml瓶装）停止销售，并将娃哈哈营养快线娄底总经销仓库内的该批次和型号的产品全部封存。 <br />
消费者唐强是投诉娃哈哈营养快线的家长之一，他两个小孩喝了有问题的营养快线后立即出现腹痛、腹泻不止，经过5天花费了2000多元才治好。10月19日，他告诉记者，报道发出的第二天，10月17日下午，娃哈哈公司娄底负责人梁经理还企图把封存的产品偷偷运走，幸亏小卖部宋老板及时报了110，在警方的干预下，他们才不得不将封存的产品送回小卖部。“没想到娃哈哈方面用这么卑鄙的手段，居然想抢走证据！” <br />
杭州娃哈哈集团有限公司售后服务部的方先生在10月16日接受本报记者采访时称，调查后两天内会和记者联系并给出解释。但截至目前，本报记者并未接到娃哈哈方面任何解释。 <br />
凤凰网调查数据显示，娃哈哈营养快线乃至整个娃哈哈品牌正面临着一次前所未有的考验，截止10月18日下午4点30分钟，有8290人参与了凤凰网《娃哈哈营养快线致病风波》的专题投票，其中有4722票近六成网友表示“营养快线太不安全，以后不再继续饮用”。 <br />
<br />
<br />
娃哈哈方面 <br />
欲将封存的问题产品“抢走” <br />
<br />
&nbsp; &nbsp;“从9月20号事情发生到现在，甚至现在媒体报道后引起社会关注，他们（娃哈哈公司）从未主动跟我联系解决问题。我能联系上的也就是一个李姓业务员，我多次向该业务员询问他们公司娄底负责人联系方式都被拒绝告知，而我每次找到李姓业务员她都是说自己做不得主”，消费者唐强介绍，更让气愤的是，10月17日下午3点左右，娃哈哈公司娄底负责人梁某带了几个人到宋老板的小卖部将娄星区工商局封存的产品拖走了。 <br />
“梁经理谎称是工商局叫他来把货搬走的，我年老的父亲信以为真就让他们把货物搬走了”，宋先生回忆，当自己回到家里听说此事，马上和消费者唐先生取得了联系，另外马上致电工商局的相关负责人得知娄星区工商局并没有委派任何人来搬封存的产品，是娃哈哈公司的梁经理骗自己老父亲的。问明情况后，宋先生立即拨打110报警。 <br />
“我现在对他们彻底不相信了，不诚心解决问题，一天到晚就想着怎么把我蒙骗过去”，经过此次波折的消费者唐强对娃哈哈多了几分愤怒和担忧，“他们肯定是想把封存的问题产品搬回去销毁证据，我现在不能保证他们是不是在封存的证据上动了手脚”。 <br />
<br />
工商部门 <br />
叫停同批次产品的销售 <br />
<br />
&nbsp;&nbsp;娄星区工商局消保股负责人覃先生告诉记者，“为了保护消费者的饮食安全，目前娄星区工商局已经下令辖区内超市、小卖部暂停销售350ml5月8日生产的批次产品，同时将娃哈哈娄底总代理老王批发部的产品进行了封存”。 <br />
覃先生介绍，已经将此事汇报给娄底市工商局，等检测结果出来后，会进一步做打算。“如果有问题，我们将立案调查，并对总经销商进行重罚，责令将这个批次产品全部招回”。 <br />
同时，记者从娄底市技术监督局了解到，“由于牛奶的检测存在一定难度，检测结果暂时出来不了，另外还需要消费者送2到4个样本过来检测”。 娄底市技术监督局检测室工作人员陈先生表示，“需对产品进行复检，三到六天后才能出检测结果。” <br />
<br />
