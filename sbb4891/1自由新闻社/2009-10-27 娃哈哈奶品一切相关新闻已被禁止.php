网络上不但对娃哈哈处理此事的方式和态度表示谴责，同时对相关职能部门也表示了担忧，广东省深圳市网友lcx888168留言称“由举报到工商局20多天都没有送验结果与控制措施，这是当地相关部门严重失职行为，也是对人民健康不负责任。很多公司产品在市面流通后当出了质量问题，首先解决方法就利益攻关政府部门，再小恩小利打发消费者，确实不行才慢慢坐下来协商，最后纸包不住火时高层才下令严肃查处解决。这也是我国的特色解决方法，所以我国产品质量长期都很难提升到让百姓与世人真正放心。” <br />
