2009年9月20日下午，湖南娄底一名唐姓消费者向娃哈哈公司投诉，称其小孩饮用营养快线后出现腹泻、呕吐等情况，要求娃哈哈赔偿1万元。娃哈哈公司本着对消费者负责的态度立刻对此事进行调查，并希望唐某能提供相应的凭证，如果医院检查后证明其小孩确实是饮用营养快线造成身体不适，娃哈哈将切实按照《消费者权益保护法》规定承担责任进行赔偿，但唐某却拿不出任何医院凭证证明小孩出现的症状与营养快线有关联。<br />
唐某提出的巨额索赔要求未被答应后，又向消费者协会提出投诉，消协在接到唐某的投诉后也对此事进行了调查，认定唐某在不能提供任何凭证证实其说法属实的情况下，要求是很不合理的。随后，唐某竟然携带喇叭、海报等器具在娃哈哈当地办事处闹事，造成交通堵塞。后被110警察带离。<br />
