&nbsp; &nbsp; 同时，广东惠州市民黄先生亦致电本报新闻热线称“购买的饮料娃哈哈营养快线,里面清晰可见一个黑色异物,首先认为是蟑螂,而厂方经理则怀疑为茶叶”，黄先生介绍这瓶饮料的包装完好并未开封,生产厂家是杭州娃哈哈集团,生产日期为2008年5月10日,产品保质期为9个月,当时是在保质期内。 <br />
　&nbsp;&nbsp;浙江绍兴市的消费者许先生投诉从2009年8月5日生产的营养快线喝出了一个个硬币，有媒体记者采访“娃哈哈”的售后服务工作人员时，工作人员却表示，自己从来没有听说过这样的事情，营养快线的生产是封闭式的，全电脑操作，不太可能会有硬币这样的异物存在。这位工作人员同时认为，这种流通中的硬币上面会有很多细菌，如果真是生产时进入瓶子，那么里面的液体肯定会被污染，从而出现变味变质的情况。而现在这些问题都不存在，加上瓶盖已经开启，硬币的来历很难说清楚。 <br />
“站在消费者角度考虑，我们也相信消费者不会进行恶意投诉，但事情确实很难追查清楚。”工作人员还表示，在这样的情况下他们只能为许先生调换一瓶，同时会派业务员来查看，尽可能把这件事调查清楚。 <br />
