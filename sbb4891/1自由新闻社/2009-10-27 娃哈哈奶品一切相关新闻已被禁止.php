<br />
&nbsp; &nbsp;“我明明打通了他们800投诉电话，他们居然这样活生生的就否认了，消费者唐强向记者重申的是，自己当初确实打了切打通了杭州娃哈哈集团有限公司的800电话并简单地反映了自己的情况，当时接线员还告知我将会及时派公司人员处理，怎么现在就不承认有这码事了呢？ <br />
