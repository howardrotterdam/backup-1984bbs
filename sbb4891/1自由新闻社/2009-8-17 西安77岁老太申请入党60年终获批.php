																											<div id="postmessage_206516" class="t_msgfont">西安77岁老太申请入党60年终获批(图)<br />
2009-08-17 06:14:26　来源: 华商网-华商报(陕西西安)　跟贴 325 条 手机看新闻<br />
<a href="http://news.163.com/09/0817/06/5GT8PIF700011229.html" target="_blank">http://news.163.com/09/0817/06/5GT8PIF700011229.html</a><br />
<br />
核心提示：西安老太太寇洁从1949年第一次口头提出入党申请到如今的60年间，先后写了10份入党申请书，日前，77岁的她终于被同意接收为中国共产党预备党员。<br />
<br />
华商报8月17日报道&nbsp;&nbsp;家住西安西门里的老太寇洁这几天逢人便笑，从1949年第一次口头提出入党申请到如今的60年间，她先后写了10份入党申请书，日前，77岁的她终于等到了同意接收自己为中国共产党预备党员的这一天。<br />
<br />
高度近视 熬两夜写申请<br />
<br />
2008年的一天，寇洁病重的老伴嘱咐她：“我走后你要抓紧时间写申请，专心把自己的大事解决了，我走了也就心安了。”两年前，当寇洁听到自己88岁的叔叔和78岁的哥哥陆续被评为“优秀共产党员”后，“埋在心底几十年的入党愿望再次像火山爆发一样沸腾”。这时她已退休20多年，第10次提出入党申请，经过前面9次挫折，还能不能被批准?寇洁曾经的顾虑被老伴的鼓励打散。高度近视和白内障，使寇洁几乎看不清东西，寇洁花了整整两夜，在2008年底第10次将入党申请书提交给了退休前任教的西安市师范学校。<br />
<br />
60年交了10份入党申请<br />
<br />
“1949年，我感受到了共产党领导的新中国给人民带来翻天覆地的变化，当时年仅17岁的我对共产党产生了最初的向往，但那年我第一次口头提出的入党申请，因为年龄小并没有被允许。”说起对共产党产生的朴素感情，满头银发的寇洁始终望着窗外的天空。<br />
<br />
1953年，21岁的大学生寇洁第一次正式向党组织提交了入党申请书，但还没等到组织批准，面临毕业的她仓促从家乡北京来到西安支援大西北建设，做了一名中学教师。后因工作调动频繁，寇洁总在入党考察没到期时，又被调往下一所正在筹建的学校。其间又因种种历史原因，寇洁前后提交的9份入党申请书都石沉大海。<br />
<br />
锻炼身体是转正前首要任务<br />
<br />
“校党委会已研究同意接收你为预备党员，祝贺你。”8月11日中午，寇洁放下学校领导打来的电话内心翻腾，面对老伴的遗像她泪流满面。<br />
<br />
寇洁所在的原西安市师范学校(现合并为西安文理学院初等教育学院)党委书记王新印说：“寇老师执著的入党信念让我们感动，别人在70多岁时考虑更多的是安度晚年，她却一直把入党当做自己的头等大事，也正是这份执著与坚持，让校党委同意接收她为预备党员。”<br />
<br />
“你这么大年纪了入党还能干啥?”寇洁不止一次面对这样的不解，她回答：“对共产主义的信仰是我一生的追求，我可以发挥余热，为党为群众做很多事，哪怕是一次助人为乐。”<br />
<br />
这几天，寇洁一直在认真学习《保持共产党员先进性教育读本》等书籍，“我的思想要时刻紧跟党的步伐，而且从此锻炼身体就成了我必须完成的一项政治任务，只有这样我才能按时转正，更多地发挥共产党员的作用。” (本文来源：华商报。 更多精彩内容，请登录华商网 <a href="http://www.hsw.cn/" target="_blank">http://www.hsw.cn/</a> 作者：郝蕾) <br />
