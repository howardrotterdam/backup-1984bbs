																											<div id="postmessage_50273" class="t_msgfont">“为什么老百姓不公布财产？” <br />
<br />
《财经》记者 王和岩&nbsp; &nbsp; [2009年03月10日 12:54] 　&nbsp; &nbsp;共有17条点评<br />
 <br />
某些官员在财产公示问题上，把自己等同于一般老百姓了<br />
<br />
　　【《财经网》专稿/记者 王和岩】友谊宾馆驻扎着全国政协委员中共组。委员们大多是前任和现任的各省市区政协主席等。他们为官多年，阅历丰富。如今退居二线，心态、胆量都非同以往。若能访到他们，没准会问出一些东东。<br />
<br />
　　然而，我的如意算盘打错了。尽管今非昔比，他们身上依然保留着往昔的派头，威严得如同盛夏的烈日。<br />
<br />
　　好在不是所有人都如此。我在宾馆守了两天，总算抓到一位人物。他，从餐厅出来，守在门口的我赶紧迎上去，一路相跟着。好在他和蔼可亲，我也就见缝插针地问：“某主席，你怎么看待官员财产公示制度？”<br />
<br />
　　他笑眯眯地回答：“很遗憾，我对这个问题没有研究。”<br />
<br />
　　我问：“新疆的阿勒泰、浙江的慈溪都在搞官员财产公示，贵省有没有意愿搞试点？”<br />
<br />
　　他说：“我不知道。”<br />
<br />
　　“这几天大家有没有讨论过这个问题？”我又问。<br />
<br />
&nbsp; &nbsp; 他边走边说：“没有。我们中共组里没有人讨论。”<br />
<br />
　　我有些不甘心：“你会提这样的建议或者议案吗？”<br />
<br />
　　他看着我反问：“不会。如果要公布，为什么不公布老百姓财产？那些企业老板的利润为什么不向工人公布？”<br />
<br />
　　“老百姓为什么不公布财产？”我几乎有些不相信自己的耳朵，赶紧问：“企业老板？你是说国有大企业的高管吗？”<br />
<br />
　　我清晰地听到他的回答：“不是，就是那些私营企业的老板。”<br />
<br />
　　我目瞪口呆，再也问不出问题。<br />
<br />
　　“降低了要求，把自己等同于普通老百姓。”这是一些贪官落马后反省自己的常用惯语。看来在财产公示上，部分官员又把自己等同于一般老百姓了。<br />
