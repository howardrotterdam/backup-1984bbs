													<h2>《凤凰周刊》：为洗清负面形象 政府耗资450亿与外媒争话语权</h2>
						
						
																											<div id="postmessage_50634" class="t_msgfont">李希光曾这样论及中国国际传播的困境：“从当前看，中国的外宣产品几乎很难进入国际社会主流，特别是难以进入西方社会主流。例如，在海外酒店偶尔会看到央视中文国际频道，但很少能看到央视的英语频道。新华社的电讯稿很少直接在西方主流媒体上落地。即使采用，往往是负面采用。而西方媒体引用的新华社信息，在西方读者看来通常是给中国带来负面形象的信息。如‘据中国官方通讯社新华社报道，中国宣判55名参加西藏骚乱的喇嘛’。”<br />
<br />
李希光认为，要克服这些传播上的障碍，可以通过新闻产品和外宣作品内容、选题、写作和制作上的创新加以改进。但他也坦承，这里面有一个难以逾越的鸿沟，涉及中国的核心利益、政治制度和基本价值体系。所以李希光建议，要提高国家软实力，中国要制造对自己更有利的国际新闻话语，制定对自己有利的国际新闻议程和国内议程；要争取“民主、自由、人权”这些基本概念的定义权，巧用更多利于&nbsp; &nbsp;议程的新闻事件。<br />
<br />
中共450亿争夺话语权(上) <br />
转自《凤凰周刊》09年第7期<br />
<br />
正当世界著名媒体受金融危机影响纷纷裁员时，北京却不动声色地推出一项大手笔的媒体扩张计划。这个宏大计划最早的触碰者，也许是大陆部分新闻专业2009年应届毕业生。2008年11月，他们在校园招聘会上意外感受到这个就业寒冬期的一股暖意。<br />
<br />
华中科技大学新闻学院大四学生李菲，因其广播电视专业，原不指望迈入新华社的大门，但在新华社各分社的校园招聘会上，却令人惊讶地出现大量电视记者和摄像记者的岗位，李菲顺利通过笔试和面试。厦门大学新闻传播学院的硕士生陈璐同样被乐观情绪包围，他同时参加新华社和外文局《北京周报》的招聘，均进入最终面试。陈璐介绍：“今年新华社和外文局都到学校开宣讲会，这在往年很少见。”<br />
<br />
素来只需要文字记者和少数摄影记者的新华社，此番大规模招聘重点放在电视记者岗位；而外文局系统紧锣密鼓的招聘范围更广，其属下的互联网新闻中心、北京周报社、外文出版社、国际图书贸易总公司等机构都有人才需求，外文局网站在其显要位置直接公布招聘咨询电话。有消息称，北京已计划投入巨资加强媒体力量，通过“话语权”扩大中国的全球影响力。此次大规模招聘应届生的新华社和外文局系统，正是这次北京媒体扩张计划的两大主力。<br />
<br />
本港《南华早报》报道：“中央政府准备耗资450亿元人民币，将分别用于中央电视台、新华社、人民日报等国家级媒体增设海外业务，以改善国家在国际上的形象。”除《南华早报》外，《纽约时报》1月14日援引中国人民大学新闻学院副院长喻国明的话说，450亿详细计划还没拟出，但主旨已经拟定，就是要改善中国的国际形象。新华社主办的《国际先驱导报》2月初刊登封面文章《中国全面启动国家公关》，侧面回应了此前的传闻。报道称：“国家有关部门日前已经全面启动国家公关战略。《南华早报》则揭开了中国国家公关战略的一角。”<br />
<br />
2008，中国形象之痛<br />
<br />
2008年．大陆从官方到民间，均深刻感受到中国国际形象遭遇空前挑战的危机。3月14日，拉萨骚乱。大陆官方媒体新华社和中央电视台的报道，几乎不被国际社会采信。西方社会对华负面报道，瞬间升温。4月，奥运火炬传递，沿途遭遇的抵制、抗议和抢夺火炬更让中国上下瞠目结舌。为保护火炬不受干扰，海外留学生发起组织强大的护火队伍，与抗议者针锋相对。在国内，掌声一片；在国外，则截然相反。4月9日，火炬经过美国旧金山，CNN素来以言辞刻薄著称的主持人卡弗蒂称：“我知道我们跟中国的关系肯定有改变。我认为，他们基本上同过去50年一样，是一帮暴徒和恶棍。”<br />
<br />
火炬不仅在伦敦、巴黎等地受扰，且被迫不断临时改变和缩短传递路线，而西方对华舆论攻击也愈演愈烈。一些欧洲国家政要突然变脸，纷纷拿中国“人权”开刀，一些政要甚至直接宣布将不参加奥运开幕式。<br />
<br />
从拉萨骚乱到奥运火炬传递，西方舆论对华的负面报道，涉及之广、程度之深、甚至超过了1989年春夏之交的政治风波。这些都让不少中国普通民众感到惊讶。他们惊讶于海外居然会对这些问题有如此强烈的反映，西方社会对中国官方观点的完全否认更让他们无法理解。<br />
<br />
中国官方和民间惊愕之余，以自己的方式奋起反击。法国连锁超市“家乐福”成为爱国标靶，人民网、新华网等官方网络媒体开展了“反对CNN等西方媒体不实报道”、“全球华人反藏独护圣火”、“强烈谴责CNN主持人辱华言论”等网上前民活动。一时间，“做人不能太CNN”，这句80后创造的俚语，其小国几乎等同于几十年前中国大地流行的一句话：“别了，司徒雷登！”<br />
<br />
拉萨骚乱发生后，因无法进入现场，3月15日赶到拉萨的奥地利《侧面》杂志记者古思亭，成为许多西方媒体的采访对象。在向同行介绍并没有发现一些媒体宣称的“大屠杀”后，古思亭深有感触，因政治制度之别，确实有不少西方新闻人一直对中国存在偏见，而大陆官方在事件发生后的应对方式，更在关键时刻使这种一贯印象被强化。<br />
<br />
加拿大人凯恩・麦卡锡曾在中国国际广播电台和北京人民广播电台的外语广播中担任编辑、主播和顾问，也是央视英语频道的顾问兼编辑。他对此深有体会，“外国人看中国，与中国人自己看中国之间，有着一个很大的差距。”<br />
<br />
在西藏动乱和美国CNN报道等问题上，中国出现的失误加深了危机感。西方媒体表现出来的中国形象，让中国领导层认识到，国力不是国家公关的唯一因素。中国经济在世界经济中有了很大分量，但中国的声音和文化影响，即软实力，却没有相应的地位。英国BBC国际台赞助的最新民意调查显示，国际社会2008年对中国的印象有转为负面的倾向。尤其是“3・14”事件过后，中国的形象跌到了极点。<br />
<br />
大外宣关键词：话语权<br />
<br />
一些大陆官方智囊数年前就不断撰文研讨改善北京国际公关形象，主动争夺“国际话语权”，近年的“两会”中，也常有加强国家软实力议案，但危机才是最有力的提醒。接连不断的重大事件，让北京进一步深刻体认到自己发声的重要。2009年恰逢“西藏民主改革”50周年及“3・14”事件一周年、汶川大地震一周年、1989年政治风波20周年以及建国60周年。这几个关键性的历史时段，加上经济危机导致大量工厂倒闭、600多万大学生就业难、千万农民工失业、群体性事件高发等诸多现实问题，无疑将再次考验中国的国家公关能力。<br />
<br />
中国智囊机构加快了献计献策的步伐。曾长期关注这一问题的清华大学新闻传播学院常务副院长李希光，目前承担着“中国文化软实力战略研究”课题，该课题是国家社科基金重点项目。在李希光看来，中国在西方媒体上是软实力失败的国家。李希光认为，中国政府的高层需要清醒地认识到，中国当前最缺乏的是自己的国际意见领袖、国际名专栏作家、国际名记者队伍。中国还缺乏第三种声音为党和政府设置议程、制造舆论，形成影响力。<br />
<br />
2008年12月，中央电视台成立50周年，胡锦涛主席发去贺电，提出“努力把中央电视台建成技术先进、信息量大、覆盖广泛、影响力强的国际一流媒体”的新要求。同年12月23日，中共中央政治局常委李长春在“纪念中国电视事业诞生暨中央电视台建台50周年大会上”发表讲话时，罕见使用了“话语权”这个词汇。李长春说：“在国内外重大突发事件的报道中，争取第一时间发出我们的声音，传播我们的观点、赢得话语权、掌握主动权。”“加快实现由传统媒体为主向传统媒体与新兴媒体融合发展的转变，由以国内受众为主向国内国际并重转变，构成覆盖广泛、技术先进的现代传播体系。”<br />
<br />
无独有偶，2009年1月1日出版的新年第1期《求是》杂志上，中共中央政治局委员、书记处书记、中宣部部长刘云山也撰文强调：“新闻舆论的竞争说到底是话语权的争夺。要进一步提高舆论引导能力，掌握话语权、赢得主动权。建设语种多、受众广、信息量大、影响力强、覆盖全球的国际一流媒体。”<br />
<br />
1月4日举行的全国宣传部长会议上，李长春指出，要“努力增强文化软实力，进一步提升中国国家形象”。同日举行的全国对外宣传工作会议上，中国对外宣传办公室主任王晨指出，要“努力增强文化软实力，进一步提升中国国家形象”。同日举行的全国对外宣传工作会议上，中国对外宣传工作会议上，中国对外宣传办公室主任王晨在讲话中强调：“要着力构建大外宣格局；要围绕新中国成立60周年这一主题，积极介绍新中国的发展历史、伟大成就及其宝贵经验。着力建设语种多、受众广、信息量大、影响力强、覆盖全球的国际一流媒体。”1月12日举行的“全国广播影视局长会议”上，广电总局局长王太华表示：“把提高国际传播能力放在更突出的位置，全面加快走出去步伐，着力建设语种多、受众广、信息量大、影响力强、覆盖全球的国际一流媒体。”<br />
<br />
在胡锦涛、刘云山、王晨、王太华的讲话中，都提到要建立“覆盖全球的国际一流媒体”。建立“覆盖全球的国际一流媒体”成为“外宣大格局”重中之重。此后，中国主流媒体雄心勃勃，为“走出去”纷纷提出建议、规划。新华社可仿效卡塔尔半岛电视台，建中国版的CNN，电视新闻频道全天候24小时播出中国人视角的英语新闻，向包括美国、欧洲、澳洲、非洲在内的全球播送。中央电视台要开设阿拉伯语、俄语频道。<br />
<br />
喻国明在接受《凤凰周刊》采访时表示，自己曾经就上述相关议题接收政府部门、相关媒体咨询。但他同时也强调，具体实施办法、投资多少、何时启动、谁来负责等相关细节都还在讨论中。<br />
<br />
中共450亿争夺话语权(下)<br />
中央媒体“对外扩张”<br />
<br />
消息人士称，中国的大外宣政策没有资金或人才的问题，目前处于讨论与规划阶段，还没有具体步骤。但种种迹象表明，“大外宣”格局第一步已经呈现，将以扩大中央媒体的海外业务为主，包括建立新媒体、增设办事处、吸纳外语人才等。<br />
<br />
经过几十年发展，中国已形成具有一定规模的对外宣传体系。该体系包括新华社、中新社、中国国际广播电台、央视卫星电视、《中国日报》等平面媒体、外文局所属刊物等几大对外窗口。其中新华社被认为是中国在海外最有影响的媒体。新华社是世界级的大通讯社之一，作为中国官方通讯社，在100多个国家和地区设立了总分设、分社。《南华早报》报道引述消息人士的话表示，“新华社的计划是扩大其海外分社，从大约100个扩大到186个”，这意味着它将在全球几乎每一个国家都开有分社。此外，新华社将创立类似于半岛电视台的新闻频道，24小时向国际观众播报世界新闻。《凤凰周刊》记者发函向新华社求证，截至记者发稿时，尚未收到任何答复。<br />
<br />
除新华社外，中国共产党机关报《人民日报》也是“外宣大格局”的重要拼图。《人民日报》下属的《环球日报》，已经决定在近年5月出版英文版。自去年12月份开始，《环球时报》英文版正在紧锣密鼓地招聘能用英语工作的专业人员，包括主编、编辑和记者。该报将成为继《中国日报》之后中国第二份国有英文报纸。<br />
<br />
《环球时报》英文版共需要英语新闻60名记者和编辑、10名外籍专家以及5名管理人员。不少驻京外国媒体的英语记者都已经收到《环球时报》的邀请，筹码是提供“极具竞争力的工资标准”。“我就被告知，普通的记者和编辑年薪都在10万元以上，资深记者、编辑能得到30万元人民币的年薪，并且还提供一套住房。”一位参加过该报招聘的记者说。<br />
<br />
中央电视台已宣布推出今年开办阿拉伯语和俄语频道的计划，目前正积极扩大其海外记者队伍，并招募外语人才。中央电视台机关党委书记张海鸽近日做客人民网称：“中央电视台的外宣人才引进，有国际国内两个通道，一是向国内招聘，2009年将招聘100名小语种人才，以满足开办俄语频道、阿拉伯频道的需要；二是面向国际招聘，英语频道早就引进外国专家了，今后中央电视台将在完善驻外记者站布局的同时，进一步采取人才本土化策略，就是聘用记者站所在国家和地区的当地人才为我所用。”<br />
<br />
中共中央组织部长李源潮在2008年12月一次讲话中，鼓励“要进一步解放思想、创新机制，积极引进海外高层次人才”。张海鸽认为，李源潮近日提倡的海外人才引进政策，对中央电视台引进外宣人才是一个良好契机，一些相关政策支持将对中央电视台的人才引进开辟绿色通道。<br />
<br />
除这三家级别最高的媒体外，《中国日报》、中新社、外文局下属的新闻门户“中国网”近期也有大动作。《中国日报》是中共建政以来第一份全国性英文日报。数名该报记者、编辑向《凤凰周刊》证实，《中国日报》将在2月底推出北美版。“以前我们在华盛顿只有一名驻点记者，现在已经设立记者站。《中国日报》未来会全球布点，欧洲版也在策划中，此外近期还有望在台湾落地。”该报内部消息人士还向记者透露，《中国日报》近期在物色同时会英语和法语，或者德语的多语种人才，应对全球化布局。<br />
<br />
由外文局负责管理的国家重点新闻网站中国网，也将承担相应的外宣任务。中国网一位频道主编向本刊透露，中国网近期将筹建一个国家级英文网站。“一开始决定让新华网或者人民网承担这项任务，后来还是让中国网（承建）。”同时中新社主办的《中国新闻周刊》其英文版月刊《News CHINA》在2008年8月正式面世。这也是中国新闻社首次出版发行英文出版物。来自中新社内部消息说，该周刊将获得中新社2亿元的资金支持。此前的2007年5月，《中国新闻周刊》日文版于东京创刊，成为首家进入日本主流发行渠道的中国刊物。<br />
<br />
谁来办“中国CNN”？<br />
<br />
不过，最受人关注的还是“中国CNN”的提法，这将是此次“大外宣”部署中最重要的一环。多位高层强调“着力建设语种多、受众广、信息量大、影响力强、覆盖全球的国际一流媒体”，无论是新建一个全新的电视台，还是最终选择在原有的电视台上扩张，可以肯定的是，要打造“覆盖全球的电视台”势在必行。<br />
<br />
<br />
此外海外媒体大量报道，建立海外电视台的任务将由新华社完成，最终与英国广播公司（BBC）和美国有线新闻网（CNN）竞争，而450亿元投资很大一部分是针对该项目的。有报道称，新华社在上世纪90年代末就曾策划过办电视台，但所需资金庞大，当时又受中央电视台“冷遇”，所以只能搞“试验田”制作一些视频节目，作为内容提供商，向各地有线电视系统播放。因无法进入央视主导的主流电视广播体系，故不成气候。<br />
<br />
新华社汇聚了中国最多的优秀新闻人才，有中国最健全和最庞大的新闻网络，有专用的卫星转发器。在当前部署“大外宣”格局下，新华社办新闻电视台的呼声再度响起。虽然消息还未获新华社证实，但新华社近几年确实在大力发展视频业务。除在应届大学生中招聘电视记者外，去年11月初开始，新华社将其驻外视频报道人员在中国传媒大学进行分批培训，培训时间持续两个多月。当被问及新华社此番培训是否为其开办电视台作准备时，负责培训计划的工作人员表示，培训属于新华社内部的工作事务，具体计划目前尚不便透露。<br />
<br />
新华社目前有“新华网络电视”，隶属负责视频报道的“音像中心”，各驻站也都有视频报道人员，同时加速了海外布局。2月4日新华社与美国新亚电视正式签署合作协议。新华社向新亚电视提供新华视频专线中的16个栏目。至此，美国新亚电视成为北美地区第一个播放中国新华社视频节目的电视台。<br />
<br />
据消息人士透露，新华社已开始物色一些出色的电视主持人和编导，并从海南旅游卫视某知名主持人和数名编导、记者。之所以会从旅游卫视挖人，就是看中旅游卫视的多次大型直播经验。旅游卫视相关人员向《凤凰周刊》证实，旅游卫视最近人员流动很大，不过，尚无法确认已离开同事的新去向。<br />
<br />
分析人士称，新华社在海外100多个国家和地区有分社，遍布全球，这是新华社最大的优势。不过由新华社办电视台的缺点也很明显，除了重新开张运作成本比较高外，新华社也缺乏视频报道经验，尤其是大型活动的直播经验。在中国，这方面最有优势的是中央电视台。2003年，伊拉克战争爆发，中央电视台国际频道、新闻频道进行不间断报道，此举被业界看作中国电视媒体走向成熟和国际化的一个里程碑，央视也称其是“为新闻频道的开播做了一次充分的练兵”。<br />
<br />
对战争等灾难性、突发性事件的直播，恰是CNN和半岛电视台等国际顶级媒体的“最大特色”。1991年1月16日爆发的海湾风暴，使CNN一举成名。当时的CNN记者Peter Arnett（彼得・阿内特）对萨达姆进行独家采访，世界各国和外交部长的房间里，电视机几乎都在接收CNN；当时的美国总统布什对外国政要说：“我从CNN了解的东西甚至比从中央情报局了解到的还多。”<br />
<br />
央视成功直播伊拉克战争后，美国《华尔街日报》评论，中国的国家电视台希望，这次伊拉克战争能给它带来上次海湾战胜给CNN同样的东西：观众的信任、源源不断的广告收入。<br />
<br />
2008年12月，央视网发布题为《中国“CNN”成立增强国际话语权》的消息。消息称，全国50家电视机构负责人在北京签署协议，成立中国电视新闻直播联盟。由中央电视台牵头组织的国内最大电视新闻资源收集和播放平台正式投入运行。“中国电视新闻直播联盟”的英文名称是“China Satellite News Gathering Alliance”，缩写为“CSNG”，成立该联盟的目的是“增强国际话语权”。<br />
<br />
不过，在新华社、央视之前，凭借第一时间、第一现场报道9・11事件、阿富汗战争、车臣战争、莫斯科和别斯兰人质事件，创造了中国传媒直播世界突发性大事件多次“第一”的凤凰卫视，也被不少舆论称作“华人CNN”。<br />
<br />
怎样才能有“中国CNN”？<br />
<br />
无论谁来担当“中国CNN”的任务，如何才能成为“中国CNN”，都是一个重要问题。李希光曾这样论及中国国际传播的困境：“从当前看，中国的外宣产品几乎很难进入国际社会主流，特别是难以进入西方社会主流。例如，在海外酒店偶尔会看到央视中文国际频道，但很少能看到央视的英语频道。新华社的电讯稿很少直接在西方主流媒体上落地。即使采用，往往是负面采用。而西方媒体引用的新华社信息，在西方读者看来通常是给中国带来负面形象的信息。如‘据中国官方通讯社新华社报道，中国宣判55名参加西藏骚乱的喇嘛’。”<br />
<br />
李希光认为，要克服这些传播上的障碍，可以通过新闻产品和外宣作品内容、选题、写作和制作上的创新加以改进。但他也坦承，这里面有一个难以逾越的鸿沟，涉及中国的核心利益、政治制度和基本价值体系。所以李希光建议，要提高国家软实力，中国要制造对自己更有利的国际新闻话语，制定对自己有利的国际新闻议程和国内议程；要争取“民主、自由、人权”这些基本概念的定义权，巧用更多利于&nbsp; &nbsp;议程的新闻事件。<br />
<br />
喻国明对此持谨慎态度：如果按照央视、新华社现在的传播模式和实力，要打造一个有区域性影响力的媒体都很难，更别说像CNN这种有全球影响力的媒体了。喻国明称，卡塔尔半岛电视台的模式值得思考。半岛电视台从卡塔尔这样一个不足五六十万人的国家，从世界上最弱小的国家发出最强大的声音，成为今天阿拉伯世界的第一大电视台。如果从全球受众的数量上来讲，即使和CNN、BBC相比，半岛电视台也不会逊色。半岛电视台虽然坚持本土立场，同时不回避自身问题，报道上也相对客观。<br />
<br />
“在相关部门征求意见的过程中，专家们提出了两点建议，以新闻的原则、市场的原则做媒体，要与受众的实际需要接轨。否则再办一个海外CCTV，没什么太多意义，投资再多，对提升国家软实力也无太大帮助。学习半岛电视台，赋予其独立报道权，强调中性化。报道世界各地的新闻，让当地受众可以通过其接收身边的事情。台址应设在国外，采取资本多样化形式，中方并不一定全资控股。台址不要放在欧美，可以是新加坡或是泰国等东南亚地区，还可以代表亚洲立场说话。”喻国明表示。而据最新的消息称，新华社将要办的这个电视台，将是一个全英文的频道，而且很可能设在海外，而不是国内。但这一消息同样被高度保密，对于这个即将出台的新频道，业内同样猜测纷杂。当然，迄今为止，这类猜测和说法并没有得到官方证实。<br />
<br />
美国《基督教科学箴言报》认为，中国的形象固然重要，但首要问题是媒体自己的形象。在不少舆论看来，中国政府的新宣传计划要取得成功，必须予心得国际电视台或报纸以自由，让它们自由地报道他们所想报道的东西，否则效果会大打折扣。如果媒体缺乏公信力，很难想象可以凭借它来改善国家形象。</div>

							
							
							
							
