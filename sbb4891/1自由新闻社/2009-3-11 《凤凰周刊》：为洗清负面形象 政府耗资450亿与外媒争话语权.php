																											<div id="postmessage_50854" class="t_msgfont">中国媒体弱势地位不是金钱可以改变的<br />
<br />
/ 章文<br />
<br />
2009-03-11 12:53 | 阅读(1019) | 标签: 中国媒体, 弱势<br />
<br />
3月10日，新闻出版署署长柳斌杰在接受媒体采访时称，“和国际媒体相比，我国媒体的弱势主要在体制。过去在计划经济体制内，媒体是按部门、按行政级别设置的，不进入市场。在这一点上，和国际媒体比较，我们仍处于弱势，因为体制内的媒体不能参与市场竞争。另外在首发权、首播权方面，我们能够影响国际舆论的重量级媒体还很少，所以中国媒体在世界范围内仍然处于弱势地位。”<br />
<br />
不得不说，柳署长所言是大实话，和国际媒体相比，因长期以来受体制的限制，中国媒体还是小儿科，远远不在一个级别上，还是第三世界和第一世界的关系。<br />
<br />
柳署长同时表示，“我国拟投入一些资金（否定了此前中国将投入450亿加强外宣的传言），加大对外新闻报道力度，以加强我国媒体在国际上的影响力。”<br />
<br />
不得不说，柳署长所言极可能是“画饼”，是“望梅”。因为，在下一步的新闻体制改革中，“一部分时政类的报刊、新闻机构还是按照现在的体制，但另外一些行业性的、非时政类的报刊和新闻机构正在逐步地转向市场，走到市场竞争中间。”<br />
<br />
人民大学公共传播研究所副所长胡百精曾就“媒体外交突破西方舆论合围”，说过一段精彩的话，“强化我方媒体的传播能力，首先要让我方媒体成为焦点议题的‘一传手’而非‘二传手’，尤其不能一出现负面事件，就首先责令自己的媒体闭嘴。舆论场就像一个大容器，你公开注入的信息越充分，其他人的意见空间和回击余地就越狭促。”<br />
<br />
在国内事务报道上，我们的媒体有天然的优势做“一传手”，但最终在诸多重大、突发事件上，甚至连“二传手”都做不上，只能在“禁令”面前选择沉默，眼睁睁地看着国外媒体抢先，对其报道中的“失实”部分也无力纠正。<br />
<br />
在国际事务报道上，我们的媒体更是无力去和国际媒体竞争，由于人力、财力、物力的有限，我们媒体的驻外机构更多的时候只能做“二传手”，编发国外媒体的“一手”消息，并且由于意识形态的缘故，这些编发的“二手信息”经常有违“客观真实”的原则。<br />
<br />
众所周知，报道国际国内重大突发新闻，是时政类媒体的主要职责。可以这样说，正是因为中国时政类媒体的“集体缺位”和“不作为”，才导致中国媒体整体在国际上的影响力疲软不振，在很多时候被不明真相的国外同行瞧不起！<br />
<br />
如果下一步国内时政媒体还是如柳署长所言，沿袭现在的体制，那么它们落后的弱势地位不可能得到丝毫改变和提升。即便中央拨款再多的经费，都是在做无用功。<br />
<br />
中国的新一代媒体人，在新闻理念和写作技巧上，都已经很接近西方新闻工作者了。我身边的不少朋友新闻业务能力卓尔不群，完全有实力和西方新闻业中的佼佼者同台竞技，以客观、真实、深入的报道，去赢得国际社会的尊重。<br />
<br />
但是，当下的新闻体制，长使英雄泪满襟，拔剑四顾心茫然。<br />
