东方网12月31日报道 今天中午，有网友在上海某知名论坛上贴出《卢湾区迎世博双语指南》的照片，《指南》中以汉语发音标注英语的“雷人”方式引来网民围观与热议，网友们纷纷回帖“俺么搔瑞”。<br />
<br />
在卢湾区窗口服务与商业发展指挥部制作的这张《迎世博双语指南》中，罗列了一些中英日常用语，其中包括“早上好”、“有什么需要帮助您的吗?”和“再见”等。可能是考虑到需要使用《指南》的市民英文表达能力有限，《指南》中还人性化地设置了“对不起，我只能讲简单的英语”和“请您稍等!我叫我同事来帮助您!”这样的对话。<br />
<br />
令网友大呼“雷人”的是，在这些中英双语对话的后面，还采用了以汉字来标注英文发音的方式，比如“晚上好”的中文标注是“古的衣服宁”、“再见”的中文标注是“白白”，而“对不起，我只能讲简单的英语”则被翻译成冗长的“俺么搔瑞，挨坎翁累死鼻科额累偷英格历史。”如此发音，别说英文“不灵光”的老爷爷老奶奶，恐怕连年轻人读起来都要多反应一会儿。不过，这种“小学生学英语”的方式还是让不少网友感觉又亲切又好笑。<br />
<br />
<br />
带着网民们的热情，东方网记者致电卢湾区窗口服务与商业发展指挥部，希望了解到《指南》“背后的故事”，对方工作人员迟疑很久后遗憾地表示，具体情况他们也不记得了。尽管有些“雷人”，但多数网民还是表示出了对《迎世博双语指南》的肯定，纷纷称赞“嗲”、“很牛”，而“俺么搔瑞”更一时间成为了回帖流行语。<br />
<br />
