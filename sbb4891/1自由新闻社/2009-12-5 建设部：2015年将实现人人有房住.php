													<h2>建设部：2015年将实现人人有房住</h2>
						
						
																											<div id="postmessage_357136" class="t_msgfont"><a href="http://gz.house.163.com/09/1204/16/5PN2S2H600873C6D.html" target="_blank">http://gz.house.163.com/09/1204/16/5PN2S2H600873C6D.html</a><br />
<br />
第十一届中国住交会在北京举行，网易房产作为特约首席合作门户为您带来全程报道。建设部政策研究中心主任陈淮就《宏观经济形势与房地产市场发展报告会》发表了精彩的演讲。<br />
<br />
一年只能有3%―4%的家庭获改善<br />
<br />
陈淮表示，从1999年到2008年中国盖了80亿平方米的商品房，我们卖掉七千万套住宅，按现在的城市人口规模我们一共有两亿的城镇家庭，理论上说我们35%的家庭在过去十年里买了新房。<br />
<br />
“但实际上，就算我们过去十年这样大规模的建设中，每年一百户家庭里也只有3.5户家庭能改善。不是宏观调控政策把房价调到70%的人买得起，我们一年就能给一百户家庭盖出70套住房来，盖不出来的，一年只能有3%―4%的家庭改善”，陈淮表示。<br />
<br />
陈淮认为，现阶段政府政策不是让70%的人在2009年或者2010年就都买得起房，而是假定有一百个家庭先买上，每年只有四套房供应，哪四户家庭优先改善,政策的作用就是要保证低端的真正住房严重困难的群体，等着结婚娶媳妇的小年轻群体优先得到改善，因此2009年关于契税减免1%，首付降低到20%，利率优惠也是保证这些困难群体优先改善。我们在今年保增长中，四万亿中拿出九千亿用于廉租房建设，也是保证低端老百姓优先改善。<br />
<br />
2015年保证人人有房住<br />
<br />
陈淮还表示，明年是“十一五”规划的最后一年，接着便进入“十二五”规划，在这样一个中期发展过程中，在2015年前，中国住房目标第一保证人人有房住，不是大多数人有房住，而是人人有房住，这就是应保尽保，扩大廉租房覆盖范围。<br />
<br />
“我们保证不了富人都住好房子，我们同样保证不了大多数人都有自己的产权，这件事需要20―30年才能办到，但是我们应当保证在未来3―5年内，自己完全无力解决住房的群体政府应该给他们做好应保尽保，我们有能力应当做到，在未来3―5年内让年轻人满足住房给予他们更多的支持和援助”。</div>

							
							
							
							
