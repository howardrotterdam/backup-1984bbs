													<h2>在疆的维吾尔人讲述当前新疆民族关系的一些案例</h2>
						
						
																											<div id="postmessage_233785" class="t_msgfont">作者：google13<br />
<br />
<br />
<br />
当前新疆民族关系的一些案例<br />
<br />
“九三事件”中的几个能确定准确细节的案例<br />
<br />
海莱特・尼亚孜口述：<br />
<br />
案例一<br />
<br />
红雁池电厂一个社会青年，维吾尔族，大学毕业几年后没找到工作。七五事件之后被街道部门找去当联防队员――又称为临时保安，每月有几百元的工资，单位管午饭。他个人觉得这个工作不错，但他当机关干部的父母不同意他去当保安，他父母认为，此时正值乌鲁木齐的动乱时期，当保安很危险。故提出几个条件，其中最主要的一条是，若当保安，应在厂区内执勤，而不能出没有安全保障的厂区。当时，找他们当联防队员的街道和延安路派出所允诺，他们不会被派出厂区之外的地方值班，只在方圆几公里内的社区执勤。<br />
<br />
9月2日，乌鲁木齐开始因为“扎针传言”有大批示威者上街（其实从8月28日就开始闹事）。9月3日早晨，联防队的领导，也是当地派出所的干警，早上7点天不亮，就把这批联防队员以出早操集合的名义拉了出去，当时大家连早饭都没吃。这批联防队员总计20个人，其中16人是维吾尔族，带队的干部是汉族。这批联防队员直接到距红雁池电厂好5-6公里远的小西门。这里是9月2日和3日闹得最凶的地方。<br />
<br />
这批联防队员到位后，他们被布置在盾墙的第一排，他们身后的第二排是特警，第三排是武警。他们面对的是几千名汉族示威者。<br />
<br />
示威者与纪律部队双方对峙不久，秩序很快混乱，警戒线被冲开了一个口子。这名维吾尔族联防队员成立集中的攻击对象，但他记得，第一棍重击是从后面打在他头上，而不是从前面打过来的。他被当场打倒在地。然后无数的示威者冲上来对他乱踩乱打。但近在咫尺的纪律部队却没人上来拯救他。<br />
<br />
这位联防队员醒来后，发现自己是躺在一幢大厦里，周围有二百来个男女，见他醒来，继续对他殴打，其中有位女性用电棒在他浑身上下乱捅。<br />
<br />
再后来，等他苏醒，发现自己躺在医院的病床上，其间的过程，他自己完全不知道。<br />
<br />
与他同去执勤的维吾尔族联防队员，都是被击中殴打攻击的目标，他们被迫脱离队伍逃命，他们身后大群示威者追赶。<br />
<br />
其中结伴逃跑的几个人，走投无路跑到一个小区，逃进一个家属楼，他们冲开门躲进四楼一个主人不在的住宅。在里面拼命反锁并顶上门。追击者在外面拼命砸门打门。眼看无法守住。他们从厨房里搬出煤气罐，宣称要打开煤气阀门与追杀者同归于尽，这才迫使那些人跑了出去。<br />
<br />
事后得知，他们这批16人的维吾尔族联防队员，有3人身负重伤，9人轻伤。他们事后怀疑，当时把这批维吾尔族联防队员调去执勤，并站在第一排，有驱羊入虎口，送给汉人泄怒的嫌疑。<br />
<br />
案例二<br />
<br />
这个案例也是发生在9月3日。在乌鲁木齐国际大巴扎的家乐福当普通员工的一个维吾尔族小伙子，家住六道湾附近。当天下午，他下班后，顺东环路往六道湾走。走到乌鲁木齐南门体育馆附近时，正好碰到一大群示威者在街上游行，他与同行的伙伴往前走了一段后发现气氛不对，因为很多示威者袖子里、腋下都有棍棒。他们脚步迟疑，这时，示威者也发现了这两个形单影只的维吾尔人，一大群人突然脱离示威游行的队伍，抽出棍棒冲向他们。<br />
<br />
这两个人立即夺路逃跑，因为附近就是乌鲁木齐武警二支队驻地，于是他们就往武警驻地方向逃命，想获得武警救助。当他们快跑进大门时，里面值班的武警突然发现一大群汉族人在追赶至两个维吾尔族人，他们立即关上大门，这位维吾尔族的同伴快一步，被夹在关闭的大门中。追上来的汉族示威者冲上来，掏出棍棒对准这两人劈头盖脸乱打。为了活命，这两人都努力把头塞进大门的铁栏杆内，死抓住栏杆大呼“解放军叔叔救命”。<br />
<br />
大院里的武警非但没有救助，反而出来把他们往外推。现场附近正好有一幢自治区宣传部的宿舍楼，外面的喧闹声让很多门窗打开，许多人伸出头来为外面打人者大声呐喊助威。<br />
<br />
正绝望之时，武警终于出来干涉，把这两人用皮带将双手反绑之后，又将两人裤子褪至膝盖部位，让他们头贴水泥地，冲着外面的示威者跪伏在地。然后几个武警轮番上前拳打脚踢。围观者大声叫好。<br />
<br />
武警殴打这两人五六分钟后，接到报警电话的幸福路派出所两名警察赶到现场，制止了暴行，要将两人带离现场。<br />
<br />
警察问武警，这是怎么回事。武警对前来解救的警察说，我们看这两个人往院子里冲，我们以为他们是想跑来给我们扎针的呢。<br />
<br />
据目击者说，打人的武警中，军衔最高的是中尉，其他基本上都是志愿兵。<br />
<br />
案例三<br />
<br />
铁列克讲述的。<br />
<br />
9月3日，铁列克的两个朋友（维吾尔族）从昌吉那边回乌鲁木齐，经机场高速方向的宣仁敦收费站附近时，被一群身着特警制服的人拦下（现在新疆一些保安的制服也用的是特警的蓝黑色制服）。车被拦下后，他们被索要各种证件检查并经过反复盘问，同时，小车从后备箱到驾驶室各处都被仔细检查一遍。未查出任何问题。这批制服人员将两人从汽车上拖出来，打倒在地，拳打脚踢打累了才罢手。这些人对这两位维吾尔人说，你们这些毛驴子（新疆部分汉族人对维吾尔族人的蔑称），我们用拖拉机把你们拉出活埋掉我们都不解气。由于知道他们的身份是干部。这些人说，你们回去可以去告我们，你们愿意上哪告我们就上哪去告，随你们的便。<br />
<br />
九三事件中维吾尔人的受伤害数目估计<br />
<br />
据海莱特・尼亚孜掌握了解到的情况，光第二医院，即自治区人民医院，9月3日前后，送到这里的受伤无辜者有二三十人，抢救不及死亡者有二人，兵团医院死亡一例，死者是被误认为维吾尔族的哈萨克族人。估计9月2-3日，被打死的非汉族人应当超过十五例，受伤程度不等的，应当超过四百例。<br />
<br />
其他<br />
<br />
在七五事件中，有很多维吾尔人勇敢地站出来拯救素不相识的汉族人，七七事件中，也有汉族人拯救维吾尔人的，但九三事件中这种事情就很少听说了。而且，汉族人拯救维吾尔人的事迹，因为舆论导向，不太可能像维吾尔人拯救汉族人一样被传播。<br />
<br />
九三事件中，维吾尔社会能广泛确认的惟一一例汉族人拯救普通维吾尔的事情，不知具体的发生地点。当时是一对年轻的维吾尔夫妇被一群汉族人围攻殴打，妻子被打倒后，丈夫扑到了妻子身上，用身体挡住了密集的拳脚和棍棒，这时，一位与这对夫妇可能是素不相识的汉族年轻人在劝阻无效后，扑在了护卫妻子的那个维吾尔族男子身上，周围的人大骂他是汉族的叛徒，然后对他也拳打脚踢，由于这个人的挺身相助，这对维吾尔族夫妇保命，但他本身被打成重伤。这个汉族人的英勇事迹可能永远不会被媒体公道传扬。<br />
<br />
二、七五事件后，九三事件前，一些说明民族仇恨急剧上升到其他案例<br />
<br />
歌手米尔扎提遇害事件<br />
<br />
米尔扎提是乌鲁木齐五中的音乐老师，在新疆是小有名气的歌手，维吾尔年轻人大都听过他的歌。他这些年连续到和田支教了四年。七五事件之后，乌鲁木齐民族矛盾公开化。没多久，米尔扎提失踪，后来发现他被人杀死在自己的汽车里，时在七月末，被发现时，米尔扎提已经遇害超过三天，尸体已经开始腐烂。公安虽然也来调查立案，但在民族仇恨的大背景下，此案也许永远是无头案件了。<br />
<br />
出租车司机杀人案<br />
<br />
这个案例是塔伊拉姆听木合塔尔讲述的。<br />
<br />
这个事件是8月28日以前发生的事情。有一天晚上，几个乌鲁木齐的维吾尔人在一个朋友家聚会。晚上聚会结束，大家分头回家，一位维吾尔族年轻女性回家的方向只有一个人，因为当时的民族气氛非常紧张，大家决定先让她乘坐出租车回家，他们拦下一辆出租车后，告诉司机她的地址，为防止意外，大家还记下了出租车的号码。<br />
<br />
结果当天晚上，该女士一夜未归。木合塔尔他们还保留有出租车的号码，于是向警方报案。调查很快找到了出租车公司，也找到了那位司机，司机是个汉族人，三十多岁。他很痛快地承认，是他杀害了那位维吾尔族女性。尸体当天晚上就被他扔进了城市排污的暗沟里。此人很平静地说，他的妻子在七五事件中遇害，所以他发誓要亲手杀掉五个维吾尔人，现在你们知道我杀了人，我再也杀不了其他人了。自始至终这个司机态度很平静。<br />
<br />
三、关于九三游行和扎针事件<br />
<br />
由于几乎没有维吾尔人敢到公共场合出现，所以都只能从汉族朋友那里转述得知一些消息。据说，游行示威者除了呼喊“王乐泉下台”“严惩暴徒”这样的口号外，还呼喊“杀光维吾尔”，“王震将军回来”的口号，此外还有“吊死努尔白克力”的口号。<br />
<br />
据一些汉族朋友介绍，参加游行示威的人，似应是改革开放后进入新疆打工做买卖的人为主，掺杂有很多的社会无业人员，一般乌鲁木齐机关企事业单位的参与者不多。<br />
<br />
9月2日，游行示威者在友好路新时代把三个外国记者打了，把他们的照相机砸了，把酒店的保安全部打伤，都扔到了水池子里。<br />
<br />
乌鲁木齐或者说全新疆民族仇恨和仇杀的情绪上升到了顶点。游行示威许多年来汉族对维吾尔族人被压抑的仇恨的总爆发，七五事件的残酷性给了他们以正当的理由，而政府则以某种默认方式给了他们宣泄的机会。<br />
<br />
与七五事件中，汉族受伤者得到政府各级官员的看望不同，目前九三事件前后的维吾尔族受害者，没有任何一个干部来看望过他们。七五事件时，各级领导干部轮番上医院慰问，努尔白克力甚至跪在地上哭着。而这次同样是无辜受害者，却无人问津，形成了鲜明的对比。<br />
<br />
这次在家门口买馕，结果被一群陌生的汉人无辜打伤的卡伊那姆・贾帕尔，今年51岁，他是自治区文联成员，书法家协会副主席，维吾尔知识界德高望重的人物。从被打伤送进医院再到现在，他单位的同事们也没有一个来看望过他。<br />
<br />
扎针事件的传言<br />
<br />
早些年北京天津等地流行艾滋病人扎针的恐怖传言时，新疆乌鲁木齐也同样流传过一阵这样的恐慌，不过程度很轻微，当时自治区卫生厅的 艾滋病防治办曾公开做过宣传澄清和艾滋病常识的宣讲，当时的效果很不错。不过，这次不同，新疆全社会上下都相信这个是真的。即使认为这阵事件是一种恐惧性传言的人，也会怀疑，是否真有人利用了这种恐惧传言。而且官方的必要和澄清一直给人模棱两可的印象。<br />
<br />
尤其是，不但汉族社会广泛相信扎针传言，连维吾尔社会也广泛相信扎针传言。但流传的却是另外一个版本。维吾尔社会的说法是，从8月28日开始，到9月3日，始终有汉族人的影子。有汉族女人在公共场合，头上蒙一块围巾，装做维吾尔族人的样子，然后伺机向周围的无辜者扎针。有人现场被抓后，揭开围巾一看，却是汉族人。<br />
<br />
海莱特・尼亚孜个人的一则见闻：<br />
<br />
大约是8月27日或28日。当天与一个维吾尔族人和两个汉族人一起吃饭喝酒，主题数谈生意做买卖，海莱特临时充当一下翻译。四个人都是关系很一般的熟人。那一男一女的两个汉人彼此之间也不熟悉一起。汉族男子姓黄，个头非常壮实，谈到扎针问题，他愤怒地说如果碰到有人扎针，我上去就把他的脖子扭断。那个女的则说，现在维吾尔人扎针的针管里面，不但有艾滋病毒或其他东西，最可怕的装有炭疽。她听说已经有很多人被炸过炭疽的针后，结果送到送到医院后，胳膊被扎的结果胳膊没了，腿被扎的结果是腿没了。海莱特以前和位于乌鲁木齐北京路的前军事医院研究院，后来改为医学研究所――新疆军区防化大队打过交道，知道炭疽这种化学武器平头百姓哪里有可能搞得到？但那位女性嗤之以鼻，说你不知道，这些用来扎针的炭疽，都是美国人空投到南疆来，给这些人专门用来对付汉族人的。<br />
<br />
中国各个民族原本就缺乏理性和逻辑的训练，阶层和不同民族的隔膜更容易造成恐怖传言，而目前新疆各民族严重的仇恨和对立，无疑是各种荒诞离奇传闻的沃土。原本有相当判断力的人，在这种大环境下，也很容易“三人成虎”，现在，扎针的传言已经上升为泼硫酸硝酸的版本，传说乌鲁木齐市的硝酸全部脱销，都是维吾尔人买去准备泼到汉族人身上的。昨天（9月7日）又传说华联大批发市场失窃了一千把斧头，说维吾尔人要在9月8日全城搞大暴动大屠杀，又传说乌鲁木齐开往北京的火车发生爆炸，有的说法是一座加油站发生了大爆炸。<br />
<br />
9月7日晚上，乌鲁木齐全城戒严，天还没有黑，这座十多个民族生活聚集的城市像一座恐怖的死城，没有人敢上街。<br />
<br />
信息不透明加上新疆地方当局完全丧失了公共信誉，只要够想象力，什么事情都可能发生。</div>

							
							
							
							
