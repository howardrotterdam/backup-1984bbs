																											<div id="postmessage_233794" class="t_msgfont"><div class="quote"><h5>引用:</h5><blockquote>原帖由 <i>饭岛爱</i> 于 2009-9-9 18:19 发表 <a href="http://www.1984bbs.com/redirect.php?goto=findpost&amp;pid=233778&amp;ptid=21764" target="_blank"><img src="http://www.1984bbs.com/images/common/back.gif" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /></a><br />
您应该想想是什么逼得一直很温驯的汉民族如此嗜血， </blockquote></div>一直很温驯的汉民族？贴一段汉民族的温驯史送给你，注意，是“温驯的”汉人之间的相斫史：<br />
<br />
被遗忘的战争：咸丰同治年间广东土客大械斗研究 刘平著<br />
<br />
《被遗忘的战争》这本书利用大量档案、官书和方志等资料，对清朝咸丰同治年间广东土客爆发的一场旷日持久的民间大械斗作了比较详细的记述和评论。作者通过多角度分析，指出除了经济因素，文化、种族（广府人与客家人）的因素是暴力发生的强大动因。　<br />
　　　<br />
读后，我觉得应该介绍这本书描写的土客大械斗的前因后果了（加上自已的主观看法）。<br />
　　<br />
起因：明末清初，珠三角是战场之一，清兵大屠广州及在珠三角地区之后，珠三角人口剧减。康熙初年，为收复台湾，实行了海禁，令东南沿海各省五十里的居民全部迁回内地。同时，粤东已开发的差不多了，人口稠密，土地贫斋，加上水土流失严重，生存环境恶化，故客家人已经在蠢蠢欲动向各地作试探性的移民了（粤西，广西，台湾）。康熙后期，清政府解除海禁，但是因为缺少开发者，加上粤东人多地少，所以鼓励粤东惠潮梅之人移民到现在的宝安，江门，中山，珠海等地。当然，广府人也是这次移民的主力。<br />
　　　　<br />
经济原因：土地有限，移民众多，加上人口增长奇快，客家人的生存环境再度恶化，开始向土人（广府人，下同）租种田地。“客家占地主”，“地主排客”恶性冲突也时有发生。至十九世纪中后叶，广东土客之间的经济矛盾已相对尖锐。　<br />
　　　<br />
根本原因---土人的歧视：同一时期，粤东的客家人也有大量移民到江西（很多是棚民），因为争田地，争水利等，也产生了不少的小冲突，为什么没有酿成像 “广东土客大械斗”那样大规模，有组织，长时间的冲突呢？其原因，关键是广东土人对客人的歧视程度令人发指。主要有以下几点：　　　　<br />
1 、语言歧视：土人谓客人的语言“南蛮结舌”、“入耳0曹0曹”，虽也“习土音”，但不忘“乡音”显得与土人不同。　　　　<br />
2、 风俗歧视，土人十分反感有“古怪”葬俗等“奇特”风俗又迷信风水的客人，视‘洗骨检葬’的‘二次葬’是极端野蛮不开化的风俗。　　　　<br />
3、 “种族歧视”，虽然客家人和广府人都差不多模样，应该属同一种族吧。但是因为语言和风俗的迥异，以及各方面的原因，土人认为客人“野蛮不开化之民族”，和福佬人一样均是“非粤种，非汉种”，甚至是非人，是“犭客”，“獠”，等。　　　　<br />
先天原因：在客家先民迁移过程中，为了对付迁移途中强盗，土匪及不欢迎他们的土著，客家人的家族均是半军事化的组织。女的均是大脚婆，包揽家务活和农活，男的都是半军事化的战士，具有极具强略的攻击性。同时，客家人十分善于修巩防御工事，土楼和围龙屋就是明证。而且客家人的家族之间，也存在着极端残忍的乡族械斗，这些地锻炼了‘客家战士们’。<br />
<br />
当然广东的广府人也不是省油的灯，历史上都是出了名的“强悍好斗”、“奸滑好讼”，其家族间，大族欺负小族，大房欺负小房，强房欺负弱房，令客家人也相形见拙。广府人也有很强的防御军事堡垒--围村。<br />
　　　　<br />
主观原因：（客家人性格方面原因）客家人因长期生活在飘泊离荡的艰难环境，及不断的武装冲突的环境中，其性格：坚毅刚强、视死如归、好斗勇猛、残忍暴烈、开拓进取、极富攻击性，且个个都想当头，结果一个头都没得当（洪杨就是明证）知耻达礼、极端的好面子，不容得歧视、极富个性、不团结，刚愎自用，这也是土客战争中客家人最终失败的原因之一。<br />
<br />
太平天国运动中，不但领导层和太平军有大量的客家人，清兵中也大把的客家人（俗称潮勇）等。　　　　<br />
广东土客战争的导火索（直接原因）：由于种种原因清末广东经济破产，清朝咸丰年间的广东天地会总暴动-----洪兵暴动（也叫红巾军暴动，起义士兵以头带红巾为记号，故也叫红巾军）（曾经席卷粤西，粤南，粤北）由于适逢太平天国等国内叛乱如麻，清政府没有兵及钱粮镇压洪兵起义，于是广东地方政府鼓励各地方政府发展民团对付洪兵。虽然当时土人客人都有参加了洪兵起义的，土客也有参加民团的。但是天地会暴动（洪兵起义）的军队以土人为主，客人不多。而镇压的民团则以客勇为主，民团中的土勇战斗力差，客勇是围剿洪兵的主力。由于官府的支持，客勇在围剿洪兵中发展壮大，一时“持功气骄”、“盛气凌人”。某些客勇以为，他们的美日子来临了，从此可以一洗当年被歧视，被奴役的耻辱，并且过上土人那样的‘美日子’。于是，在部分地方，出现了客勇抢掠土人财产、霸占国产及残杀土人的事件。面对客人的突然“崛起”，土人感到万分的惊恐，忙组织反抗。土人中的“有识之士”（实为害群之马）也在造谣说“客人要反客为主”，要“铲绝土人”，绵延十几载，屠杀过百万的举世罕见的广东土客大械斗从此拉开了序幕。<br />
一开始，是客勇们攻寨掠地，在广东各地到处进攻，然后是程胶着状态，互有攻守，最终，广府人凭借人口多，经济雄厚（可以雇佣湘兵，甚至英法军队），加上官兵的助攻下，客人全线败退，逃的逃，迁的迁，大多数都被杀或饿死。　　<br />
　<br />
据史载，客勇们是相当的残暴勇猛的，他们的残暴，主要体现在，破村屠村，基本不留活口。他们的勇猛，主要体现在，面对着几万官兵的围剿，以三四十人为单位，冲进前进中的官兵群中，横冲真突，以此来阻击官兵对客寨的进攻。大多数情况下，都是弹尽粮绝才投降的。曾有开平土绅谭三才用钱诱骗四十英兵用枪来打客寨，一上岸，这几十名英军就几乎全军覆灭了，大部被客家人杀掉。但土勇们的战斗力虽不如客勇，但也不是省油的灯，残暴一点也不逊于客勇，每打下一个客寨，也是照样屠杀。一时间，广东各地，死尸遍地，惨不忍睹。　　<br />
　　<br />
但是，在战争中，客家人和广府人的死法有点不同：广府人因为有大后方，所以土寨被破之后，能逃的人都可以得到其族人的荫佑。客人的寨子如果被破，能逃离的人，因为没有后方，饿死者众。战争中，大多数土人是死于屠杀中，而大多数客人死于饥饿。而且由于因为客人没有后方，为了生存，客勇们到处抢掠，这也是客人得不到官府支持的原因之一。客家人得不到官府的支持，原因除了为了生存，到处抢掠外，还有就是客家人在广东的政治影响力远逊于广府人。虽然刚开始的时候，广东政府因为客勇们剿匪有功，而有点放任客家人当地主，但后期，受到广府人的压力，包括广府官员的上讼，广府人不断地上讼（因为客人不近省府，上讼者较少），广府豪强的经济压力及贿力，官兵的天平终于偏向土人。官兵的参战是客人失败的重要原因之一，但不是唯一的原因。经济力也是重要的原因。<br />
<br />
　　其实，作者认为，根本原因是广府人的歧视，是很有道理的。那时候，土人不但视客人为‘野蛮不开化的民族’，‘非粤种，非汉种’，还说客人是‘犭客’， ‘獠’，等。视为非人类了。显然比现在某些广府人称外省人为“捞佬”，“北佬”，“捞松”，“捞头”等要恶毒的多。作者还认为，其一切的源的均来自客家人的独特的语言，客家人独特的风俗与土人格格不入。所以，广府的一些地方的地方志说这场械斗是分声械斗，即因为语言不同而产生的战争。有趣的事，当时土人说客家话“入耳吵吵”，说广府话是“中原古韵”。客家人反讥土人的语言“隔县或隔几里路就不能通晓”而客家话“虽因地势及河流的不同有不同，但不同省份客家人交流，八八九九可以通晓”。而且客家话“最合古韵”。可见，广府人和客家人都是偏执的‘大汉族主义者’，互不服谁。<br />
<br />
土客械斗十二年，红巾军起义失败之后，广东清政府大规模地捕杀天地会起义农民。于是地主阶级为了本身利益，又挑拨起土人与客家人之间的械斗。这场械斗从咸丰四年（公元一八五四年）起于鹤山，延及开平、恩平、高明、新宁、阳江等县。原来，广东的土人和客家人都由中原迁来，只因迁来的时间有先有后，先入为主，后至为客，故有土客之分。<br />
<br />
自雍正年间，惠州、潮州客家人迁到新宁、开平垦殖以后，土人与客家人和平相处，达百余年之久。咸丰四年，广东天地会红巾军起义。这时，鹤山县许多农民参加红巾军，无分土客，同心戮力打击阶级敌人。他们很快就攻下鹤山城。当时，客家地主高三的幼子被杀，他不惜倾家泄愤，与秀才张宝铭推出武举马从龙为领袖，募集客家壮丁与红巾军为敌。这支地主武装，后来协助清兵收复了县城，并在江门、长沙等战斗中得胜，擒杀了鹤山红巾军领袖大鲤鱼、何困仔等。由是省清政府嘉奖客勇勇敢，并令鹤山知县统率客勇清除红巾军余党。当时，清兵和客勇进入各村搜捕红巾军，乘机洗劫财物，伤害及土人中的地主阶级的利益。因此，鹤山土人中的地主阶级扬言“客民挟官土”，发动土人起来报复。械斗于是开始。同时（咸丰四年十月），恩平客勇也因协助官军平定红巾军而掌握了部分地方实权，使聚居鸡啼营、尖石、夹水等地的客家人，凡佃耕土人的田，都抗拒不交田租。这一来，直接影响了土人中的地主阶级的利益。土方地主为了维护自己利益，并进一步霸占客家人的村居和田产，就煽动土人“逐客”。因此，在咸丰五年，土客械斗迅速发展到开平、恩平、高明；六年，又波及新宁。新宁的土客械斗始于那扶。原来，在咸丰四五年，新宁的土人与客家人见到鹤、开、恩、高等县械斗惨剧，触目惊心，已互相协约和好。到五年二月，开平土客械斗在邻近新宁的赤水惨烈地进行着。而新宁县邻近赤水地区的三合――大隆洞――深井――那扶一带，正是客家人聚居最多的地方。这时，新宁县的土客双方，互相疑忌。土人在地主当权派李维屏、陈郁良的挑拨下，于上泽设立均和局，于冲蒌设立升平局，于海晏设立捷胜局，编练壮丁备斗――后来又成立了指挥全局的宁阳局。客方地主当权派钟大镛（武举）和郑容等，也在深井圩设局，编练壮丁备斗。到三月，双方终于在那扶一带打了起来。嗣后，互立营寨，互相报复，互相烧村抢财物，互相杀人掳人，很快就波及全县，并与开、恩、高、鹤等县的械斗连结起来。那时以知县洪德芳为首的新宁县政府，倒向土人一边，并没有进行积极的调解，反在火上添油。　<br />
　　　　　　　　　<br />
新宁的土客械斗，一开始就非常惨烈。从咸丰六年三月至七年二月这一年间，死人数十名以上的械斗有二十多次；其中咸丰六年五月的恩平松柏山械斗（附近三县土客会斗），六月的大门（深井地区）械斗，七年正月的大隆洞械斗和丰江――白石械斗，每次死者达千多人以至四千人。经过这一年的械斗之后，在土人多客人少的四九地区，客家人站不住脚了，纷纷西迁三合、深井等地，或先迁都斛的莲花山、员山头地区，后来又西迁深井地区；而原住在田头、杨梅地区的土人，也不堪客家人的攻击，北迁都斛。<br />
　　　　　　　　　　<br />
土客械斗是断断续续地进行的。咸丰七年三月至八年六月的一年多的时间内，双方只有小接触，情势已经缓和了。可是，在地主买办阶级的组织下，不久又把械斗的火焰鼓吹起来。看！《赤溪县志》附录的同治四年六月总督瑞麟和巡按郭嵩焘给皇帝的奏摺说：“土民驱逐客民起于开平谭三才，各县从而效尤，大都拦截其辎重，占据其田产，因以为利。”这是事实。咸丰八年七月，开平籍香港富商谭三才阴谋占据恩、开、新边区客家人的田产，从香港买回一批红毛快枪，联合这个地区的绅士设立“万全局”。他们招募来外县流氓数千人，配合土人二万余人，分队出击，打破了恩开边境和新宁的那扶、深井、大门、三合以及赤水、东山等地的许多客村。这时，客家人又起来抵抗和报复，击破土村二百余条。至十月，谭三才又与都斛绅士成立“伟烈堂”，招募外县流氓几百人，配合土人三千余人，进攻客家人聚居的曹冲。于是，东面又起火了。　　　　　　　　　　<br />
咸丰九年（1859年）正月，香港英兵（大部分是汉人或印度人）驾驶汽船到赤溪，上曹冲勘测地形，侵犯了我国的领土主权。当时，曹冲居民突然见到大队的外国兵登陆，纷纷走避。这件事传到洋买办谭三才耳里，以为客家人害怕洋人，可以借助洋人来压倒他们，就以失窃案件（未详）为借口，引狼入室。他与香港英当局约定于五月二日联合行动，从南北两面夹攻曹冲。谁知急不及待的英兵，竟于五月一日预期乘舰到达赤溪，由角咀登陆，迳自向曹冲攻击。驻在曹冲南营的客家壮丁奋勇迎击，于深湾海边打败敌人；另一队壮丁又乘泥板滑行，取海滩捷径直奔角咀，截断敌人退路。这一战，打死英兵三十余人，又生擒了十余人，缴获洋枪四十多支，来犯的敌人无一漏网。第二天，谭三才指挥伟烈堂人马分水陆两路杀来，又被客家人设伏打败，遗弃洋枪百余支而退。于是，赤溪人大获全胜。他们把俘虏的英兵狠狠地教训一顿，才让他们驾舰回去。后来，伟烈堂于七月和十一月，又派大队人乘船向曹冲和田头发动两次进攻，但都以失败告终。　<br />
　　　　　　　　　<br />
械斗中，土客双方人民都蒙祸，唯有官僚地主坐收渔人之利。寨门地区的械斗结果就是这样：土客械斗最初的几年，寨门地区客家人势大，绝大部份土人逃亡阳江。八年十二月，土人绅士容休光等，在阳江与阳江局（土人械斗机构）联系起来，招募阳江壮丁万人打回寨门。到九年二月，把客家人全部赶到那扶、赤水等地去了。按新宁与阳江土人绅士的预约，应把全部客家人田地交给阳江局作酬劳（新宁知县批准）。可是，广东省清政府这时又下令将客家人田产充公，迫使寨门土人出钱六千贯和交出田地四百六十六亩给阳江局。清政府官吏把一纸命令得来的大量客田投充了，就捞了一大笔钱　　　　　　　　　　械斗中，许多地方的客家人寡不敌众，连年被迫迁居曹冲的甚多，逐渐把曹冲的荒地开垦了。咸丰十年冬，宝安县客方绅士李道昌等，率领壮丁千余人到曹冲支援，巩固了曹冲。十一年，客方绅士扬梓楠和吴福堂等又率领大队壮丁连同眷属移驻赤溪、田头，筑寨护耕。从此，客家人控制了整个赤溪半岛。这一年三、四月，鹤山、高明、恩平、开平、阳江各县土客讲和，新宁县西路土客停止了械斗，东路土客也获得了协议：“划界西自冲金咀直抵海滨，东自鼠山咀直抵海滨，凡界内属潮居、矬峒两都土产悉归客民；其冲蒌、四九、五十等处客产属归土民。”到十一月，赤水、深井等地客方富裕户四千多人，闻赤溪可以安居，集中大门乘船东迁。出海，突遇海盗陈列仔（海晏人）打劫，被杀及封闭舱内死者达二千余人。<br />
　　　　　　　　　　<br />
同治元年春天，土客恢复械斗。八月至十月，新、开两县土人万人联合攻占了那扶一带的客村。客家携男带女，向深井、大门逃去。十一月，客方绅士汤恩长（秀才）等设立福同团，以统一西路客家人武装的指挥，汤恩长为团长。十二月，他带领壮丁三千余人，护送难民东迁曹冲。他们在广海城郊的西村一带，与土人开展剧烈的械斗。明年年初二，汤恩长指挥客勇攻占了广海城。这一役，单是土人就死了四千余人。于是省清政府以客家人攻城略地为大逆不道，于三月间派兵五、六千名到广海，分水陆两路驻扎，围困广海城。至七月，城内客家人缺乏粮食，开城出走，遭到官兵及土人的截击，死者千余人；其余逃回深井大湖山。<br />
<br />
在此之前不久，广西也发生了土客之争，与广东不同，广西的“土人”不全是广府人，还有壮民以及广西的桂柳人（曾经在修订前的文章说广西的“土人”也全是广府人，这其实是错误的），广西的土客械斗，与广东的战况而截然不同。广西地贫民瘠，环境恶劣，导致民风彪悍，其民风的强悍程度要远强于广东，在广西境内，无论是广府人，还是桂柳人和少数民族，都极为善战，自明代素有“广西狼兵雄于天下”之称，客家人在广东那里表现出很强的战斗力，但是与广西本地土人的战斗力相比，客家人则要逊色不少。<br />
<br />
说起广西的土人，就不得不说“桂柳人”，其实当时广西并没有这个说法，是后人加上去的，从字面上看当然是居住在桂林和柳州的人，其实所谓“桂柳人”是指广西境内一切说西南官话的人，此一类广西土人，最初大规模迁入，可追述到明朝开国，数十万明军从四面八方进入广西，此后与当时的广西本地人融合，渐渐形成了这一类广西土著人------桂柳人。桂柳人十分善战，两广官员曾经指出，桂柳人极能战，恬不畏死，冲锋陷阵，无所畏惧，视自身为无物，其坚韧程度，世所罕见。<br />
<br />
广西的土客械斗是太平天国的起因之一：广西的土客械斗指清道光（1821～1850）末年浔州（今广西桂平市一带）地区客家人与土人的械斗。明清两代客家人从广东、江西、福建迁到浔州府渐增，主要是从广东的钦州（现已属广西）一带往北进入广西，在广东钦州，客家人与两广交界的广府人和壮民交手，战场上其实双方不分胜负，但是清政府为了缓和矛盾，把钦州一带的广府人和壮民向广西一带移民。随后客家人自以为“得胜”，妄图向北扩张，随即与广西本地土人发生了剧烈的冲突。<br />
<br />
广西当地人把这些客家人称为来人。广西土人是在浔州落籍较久的居民包括操白话的汉人和讲壮话的壮民等。来人与土人杂居一方，历史上就有隔阂，往往因土地、山林、水利、风水、坟山之争，或男女关系、口角争吵而激发矛盾，同族相帮，导致械斗厮杀，世代相仇。1850年10月，贵县（今广西贵港市）发生大规模的土客械斗，双方有数万人卷入，实力相当，根据史料记载，此次械斗，客家人一共有3万人投入，而广西本地土人也有2万余人投入（曾有文章说客家人人数处于劣势，其实是错误的，客家人在此次战斗中，人数应该还略占优势，这在清两广地方官员在1850年的奏报朝廷的奏折中有记载），双方互相厮杀40多天。客家人渐渐势弱，被广西本地土人击败，广西本地土人对客家人展开了大规模的杀戮和驱赶，一时间，客家人屋宇被焚，无家可归，生命无保，超过10万多客家人几乎被广西本地土人杀光，适遇金田起义，数千幸存下来的客家人即奔赴金田参加太平军起义。从此，广西的土客械斗结束，客家人往后也不断进入广西，但是不敢再向广西本地人挑衅，广西的土客矛盾，渐渐缓和，但是广西政府为了防止少数民族起义，强行把少数民族内迁山地，而同时利用桂柳人、广府人和客家人共同牵制防止广西少数民族叛乱，使得广西的“土客械斗”停止后，又不断发生汉族和少数民族之间的矛盾和械斗，这是后话，也不是本文讨论的内容，反正从此以后，广西的客家人和广府人、桂柳人达成了共同的团结。再往后，广西的民族矛盾也全面缓和，出现了原先的桂柳人会说白话、壮语、客家话；客家人也会说桂柳话、白话；广府人也不断有人会说桂柳话、客家话、少数民族也开始会说客家、桂柳、白话；民族之间语言已经渐渐融合，桂柳人、广府人、客家人、少数民族之间，也已经渐渐融合，出现了比如说桂柳话、白话的少数民族，以及会说少数民族语言的广府人、客家人等等，广西的内部已经开始融合，但是矛盾也开始多元化和复杂化。<br />
　<br />
广西的土客械斗结束后，广东的“烽烟”又起，从1856年起，在广东省台山县发生了持续12年的“土客械斗”（“土”即讲粤语的原住民，“客”即讲客家方言、从外地迁徙而来的居民）。这场械斗波及恩平、开平、阳春、高明、阳江、新宁、高要等县，因械斗造成的死亡人数就有两三万人之多。械斗双方抓获的俘虏，不少被押往香港、澳门，再卖往美洲充当“猪崽”华工。仅就客家人而言，被土人掠卖或自卖到南美的即达二三万人，其他不堪械斗之苦的农民，亦大批逃往香港、澳门和海外。<br />
<br />
