视频截图<br />
<br />
　　红网长沙9月2日讯 继网友爆出央视推介播放的少儿动画片《大嘴巴嘟嘟》照搬日本动画片《蜡笔小新》后，近日，在百度贴吧、网易论坛及红网论坛等网上流传的一篇热帖指央视少儿频道热播的另一国产动画《心灵之窗》也有抄袭嫌疑，很多画面和日本知名动画制作人新海诚的作品《秒速五厘米》极为相似。<br />
<br />
　　据网友发出的数组视频截图发现，国产动画《心灵之窗》与日本动画《秒速五厘米》的场景几乎完全一致，只是作稍许的改动，如把一个人面前的棍子移除，减少一些云彩，挪动一下街上行人的位置，翻转一下场景，减少几件家具，换一下鞋子的颜色，这和之前的《大嘴巴嘟嘟》抄袭《蜡笔小新》行为是基本一致的，可谓大相径庭。<br />
<br />
　　“这样抄袭太明显了，还用得着创作吗？这样的国产片也能叫德育片？”不少网友跟帖对国产动画《心灵之窗》的不满，认为“这欺骗了广大儿童的智商，抄得也太过于露骨了，将山寨精神发挥到极致了”，除了质疑，也有不少网友呼吁国产动画急需质量提升以及增强原创性，这样才能在政策宽松的墙体下推动中国动画的发展，比如多一些像《喜洋洋》这样的深受大众喜爱的动画，是再好不过的了。<br />
<br />
　　为了保护国产动画片，早在2006年9月，广电总局出台规定，要求全国各级电视台所有频道在每天17：00到20：00点的黄金时段内必须播出国产动画片或国产动画栏目，而不得播出境外动画片，到2008年，这个时间更是延长到了21：00，自此，国产动画有了量上的巨变集数上甚至超过了邻国日本。<br />
<br />
　　国产动画《心灵之窗》分为上下两部，共52集，每集8分半钟左右，该剧以动漫的形式，通过一个个富含人间真情、人生哲理的精品故事，弘扬和宣传了中华民族优秀文化，反映人们美好的道德情操和健康向上的人生观、价值观，让广大观众特别是未成年人从中得到人生的感悟、心灵的净化和美的享受，为广大青少年的成长提供一份“心灵鸡汤”。<br />
<br />
　　日本新海诚动画作品《秒速五厘米》，时代从1990年代后半到现代的日本，故事发生的地点随着少年的人生的旅程而沿着东京和其它几个地方变迁。 “我们只是尽量坚持实地取景，真实的表现生活。尽管缺少了波澜和戏剧性场面，但作品中同样装满了世间的各种滋味以及生活的美丽。”新海诚如是说。<br />
