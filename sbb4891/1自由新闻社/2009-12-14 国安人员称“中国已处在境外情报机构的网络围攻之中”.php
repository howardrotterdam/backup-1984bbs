													<h2>国安人员称“中国已处在境外情报机构的网络围攻之中”</h2>
						
						
																											<div id="postmessage_372343" class="t_msgfont">境外网络间谍窥探机密 我国深处网络围攻之中<br />
2009年12月13日09:21环球网-环球时报 程 刚<br />
<br />
木马程序死盯涉密电脑 情报机关大搞网上策反　对华网络间谍攻击无处不在<br />
<br />
　　● 本报记者 程 刚<br />
<br />
　　网络谍影窥探中国机密<br />
<br />
　　中原古都，邙山之阳，一个参与中国海军潜艇科研项目的军工科研所发生了重大泄密事件。多份重要保密资料和文件，甚至一些关键材料的绝密技术资料，都落入境外情报机关之手。谍影憧憧，黑手何在？安全、保密等部门迅速查清了案情：原来又是境外间谍机构无孔不入的网络窃密攻击。隐藏在伪装外衣下的网络间谍工具寻隙钻入一台违规上网的工作电脑，将其中存储的大量涉及军工项目的文件资料搜出、下载、传回。难以估量的军事情报损失就在看似平常的“小疏忽”中酿成了。<br />
<br />
　　“像这样的对华网络间谍攻击可谓无时不在、无处不有”，一位长期从事网络安全保密工作的官员告诉《环球时报》记者，就连很多党政机关的领导部门，还有参与重大国防、科研项目的要害机构和保密单位的电脑，都留下了境外间谍机构网络窃密的痕迹。大量涉密资料在互联网上外泄，某些单位的工作电脑已经被网络间谍工具长期控制，个别重要机构的工作电脑中甚至被植入了十几种境外特种机构的间谍木马。这位安全官员说，利用互联网攻击、策反和传输来获取中国情报，已是境外情报机构对中国开展间谍活动的一种常规手段。<br />
<br />
　　间谍网攻无孔不入<br />
<br />
　　据那位安全部门官员介绍，从掌握的情况来看，中国已经处在众多境外情报机构的网络围攻之中，而网络间谍围攻是全方位、全天候的，攻击面极其广泛。他说，作为一个正在崛起的大国，中国内部的所有情报，无论涉及政治、战略、军事、外交、经济、金融，还是民族、科技、教育、卫生……境外情报部门没有不感兴趣的，网络间谍工具因此也就无不搜寻、无不窃取。当然，网络间谍攻击的重中之重还是军队、首脑机关、军工科研和制造单位，因为什么时候都炙手可热的还是中国的战略、国防和军工情报，像军事部署、武器装备、国防高新技术等等。<br />
<br />
　　一位参与案件调查的官员告诉《环球时报》记者，中原古都发生的那起军工科研所网络泄密案的当事人姓彭，是一名科研人员。去年中秋前，他用工作电脑上网查阅自己的邮箱时，收到了一封“国防科工委办公厅的中秋贺卡”，他没有多想，信手点开，结果一下子就中了网络间谍攻击的招。那封邮件完全是伪造的，捆绑着某境外情报机构特制的间谍程序，一经点击就控制了彭某的工作电脑，偏偏彭某的电脑中还违规存储了大量军工科研项目的资料，结果，连潜艇隐身材料这样的军工技术机密都被间谍程序从网上窃走了。<br />
<br />
　　这位负责具体调查的官员向记者介绍了不少类似案件。他说，只要和党、政、军或者重要行业、重大项目沾上一点边的人和单位，其电脑就可能处在境外特种部门的网攻窃密威胁之中，稍有机会间谍程序就会悄然侵入。中国南方一个椰风海韵的地方，某民营公司经常承接军方一些武器装备的维修生意。既然是维修，就需要以所修武器的具体技术资料为蓝本，虽然公司和军方签有保密合同，但是为了方便，这家企业的相关人员把维修对象的资料都放在了工作场所的电脑里，而这台电脑又经常上网，网络上四处窥探的间谍工具很轻易地就从这台电脑里取走了部分我军舰载武器的图纸、数据等技术情报。<br />
<br />
　　大学、院所疏于网防<br />
<br />
　　孙教授在湖南的一所大学任教，他在北方一所著名的军工大学读博士时就参与了一项军事工程的重要科研课题，毕业后仍然担负相关课题的部分研究工作。作为学者，孙教授经常参加国内外的一些学术活动。了解该案案情的一位官员说，境外情报机关的间谍程序正是随着一封国际学术会议的电子邀请函进入了孙教授的电脑，结果，他根本不该存在手提电脑里的重要军事武器项目的科研文件很快就被传送到了境外间谍机构的电脑里。<br />
<br />
　　相关部门负责网络安全的官员告诉记者，大学和一些学术机构网络泄密比较严重。一些学者参与国家重大课题、重要科研项目，还有一些学者是政府高层决策部门经常咨询的专家，但他们的网络保密意识比较淡薄，不少人图工作方便，很多机密文件都存在随身携带、常常上网的电脑里，几乎等于向境外情报机关敞开泄密之门。<br />
<br />
　　周总工程师是享受国务院特殊津贴的专家，在能源化工的某个领域中是西南地区的学术带头人之一。今年年初，周总工程师的电子信箱中收到了一封新年电子贺卡，乍一看，是他的一位教授朋友所发，但就在他点开这封信的时候，却把自己电脑中涉及22个省的多个重大能源化工项目，特别是新能源项目的详细资料文件拱手送给了藏在这封邮件中的间谍程序。网络安全检测发现，周总工程师的电脑已反复被植入了3次间谍程序，仔细一查，那封电子贺卡的发件信箱与周总工程师朋友的电子邮箱只有一个字母不同，是境外情报机构的网络攻击者玩弄的一个障眼花招。<br />
<br />
　　有禁不止说明重视不够<br />
<br />
　　要杜绝网络泄密，最有效的办法就是让有保密内容的电脑和互联网物理隔绝，按那位安全官员的说法，原则就是“上网不涉密，涉密不上网”，再加上“涉密电脑不得使用移动存储介质”。让国家安全和保密部门感到担忧的是，虽然绝大多数重要单位都制定了符合这类网络保密原则的严格规定和制度，网络安全检测却仍然发现很多单位都普遍存在网络泄密的情况。一位安全研究学者对《环球时报》记者说，这反射出的是网络国家安全并未引起足够的重视。事实上，并没有多少因为违规而造成网络泄密的当事人受到严厉处罚，更没有几个泄密单位的领导因此而被追究管理、监督不力的责任。他说，这种情况已经在迅速改善之中，特别是正在修改中的《保密法》将极有可能在网络保密方面加大责任追究的力度。<br />
<br />
　　网络策反随用随弃<br />
<br />
　　与网络攻击窃密相比，在网上寻找、拉拢、策反中国国内的网民，让他们按照情报机构的指令去收集国内情报，间谍行动的目标和指向都更加明确。国家安全机关的一位官员向《环球时报》记者介绍了数起这样的案件。他说，从这类个案中很容易看出，在境外情报机构眼里，那些被诱骗、被发展的国内网民不过是最廉价的、可以随用随弃的网上情报提供者而已。<br />
<br />
　　2008年冬天，在东北的某个重要港口城市，一个刚丢掉了公司职员饭碗的34岁男子在上网找工作时被一个信息员的自由职业吸引住了，在网上两头一联络，对方只试探了三言两语就径直对他说，这个城市郊区的某某地方部署了解放军的导弹部队和阵地，可以去实地看看，然后把见到的情况记录下来，画个示意图，扫描一下，从网上直接发过来，很快可以得到相应的报酬。这个姓王的中年人竟一点也不犹豫地应承下来。他去了那个地方，找到了部队的营区、阵地，虽然只是把周围大致的环境地形、道路和部队的营房、哨兵位置等等画了个草图，扫描之后通过网络传送给了对方，但还是触动了法律红线，没过多久，王某就被捕了。<br />
<br />
　　“要想得到高额的稿费，就需要收集未公开的有价值材料”，看到“夏经理”在网上发过来的这句话，成强的心里“咯噔”一下，他知道自己很可能是跟境外的间谍打上交道了。成强是黄海之滨一个大城市的政府工作人员。前不久，他在网上看到了一则“招聘网络写作人员”的广告，按照所留的电子信箱，成强发去了一篇领导讲话稿。没几天，署名“夏经理”的人就回信了，说公司的业务主要是编发大陆的新闻，他发去的讲话稿比较对路，希望能再发些材料和他的个人简历过去，好决定能不能建立长期的合作关系。成强照办了，对方很快通知他可以长期合作，要他提供一个银行卡号以便汇稿费。此后，就来了“夏经理”的这封信。成强考虑了一番，回话说自己不想干了。“夏经理”见状赶紧抚慰，接二连三地在网上过话给成强，核心就是一个意思：网上传输出不了事，而且稿费也是相当可观的。禁不住“夏经理”的“好言相劝”，成强上了套。依照“夏经理”的点拨，他还购买了扫描仪、照相机，复制了不少红头文件和内部资料传给对方。银行卡上进账了数千元汇款后东窗事发，成强被国家安全机关抓获。按照法院判决，他必须服10年有期徒刑。<br />
<br />
　　一位专业人士说，只要被拉下水，按照境外间谍机构的命令传送情报，性质就非常严重了，是与国家为敌。业余间谍想在和专业机关的较量中侥幸脱身，是没有可能的，对间谍行为的法律制裁非常严厉，哪个网民犯了事，国家受损，他个人必然付出惨痛代价，得利的只是境外敌对力量。(环球时报)</div>

							
							
							
							
