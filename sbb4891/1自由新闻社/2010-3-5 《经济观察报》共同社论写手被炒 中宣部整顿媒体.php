													<h2>共同社论吁撤户籍制度 内地传媒为民请命可嘉</h2>
						
						
																											<div id="postmessage_507533" class="t_msgfont">明报网上版<br />
共同社论吁撤户籍制度 内地传媒为民请命可嘉<br />
<br />
【明报专讯】内地13个媒体3月1日同时发表一份共同社论，猛烈抨击实行超过半个世纪的户籍制度，「中国受户籍制度之苦久矣」，敦促官方加快户籍改革，最后把这种「已到非革新不能平息民怨」的制度彻底消除。这是中共建政60年来，除了在中央有关部门统筹下的官方口径社论以外，传媒自发的一篇共同社论，十分罕有，反映户籍制度引起的巨大民愤。然而，更应指出的是，内地传媒在事件中争取成为真正的人民喉舌的自我期许和提升，令人激赏，值得鼓掌。<br />
<br />
13媒体异口同声鸣不平<br />
<br />
正义使命超越了党性<br />
<br />
中国大陆的户籍制度是1958年的产物，简言之，是把居民分为农业户口和非农业户口。这条线一经划下，中国人民便分为两等，农村人口要转到城难之又难，就是勉强在城找到工作，但孩子的学业以至全家的医疗，都必须付出比城人更高昂的费用。这种苏式极权制度下的户籍制度，是计划经济的产物，可是今天中国早与计划经济说再见，实行的是社会主义场经济，但户籍制度却窒碍了经济发展和近年中央推行的城化大计，硬生生把人民分成两等，造成巨大的社会矛盾。<br />
<br />
中国不是世界上唯一实行户籍或户口制度的国家，事实上，日本也有类似制度，但日本允许居民来去自由，东京居民到大阪生活不会低人一等，九州岛民众到北海道工作，也不必因为户口不在当地而低三下四。但中国户籍制度却令得户籍不在当地的人变成二等公民，中共建政伊始便强调人民翻身当家作主，可是户籍制度却把农村户口和城户口分开，不啻是自相矛盾。<br />
<br />
这次共同社论事件带来的另一层意义，比起其内容更为深远。在中共治下，笔杆子和枪杆子同样重要，枪杆子可以出政权，笔杆子则是中共多次政治运动中不能或缺的利器，因此军队有政委管理部队思想，舆论部门更是一管到底，部分传媒更是直属党委，是名副其实的党的喉舌。所以，这次在一向严密监控的传媒出现跨省的13媒体共同社论，彰显了两点s（1）对户籍制度的不满不仅一省一，而是已发展成为多省的反弹，足见户籍制度已到了共同社论说的那样，「非革新不足以与时俱进的境地」的迫在眉睫；（2）内地部分传媒不畏负上严重的政治代价和后果，勇于为民请命。<br />
<br />
这不是一篇急就章的社论，应该说这是一篇构思得极为细致文章，除了内容有理有节掷地有声，其出台时间更在今年全国人大政协两会开幕前夕，本来足以成为两会主要议题之一，会被视为直接挑战中共管治的行动。之前所述，中共对笔杆子看管甚严，这份共同社论肯定是在高层部门睁一眼闭一眼之下推出的。不能排除的是，这些媒体走到这一步，已是在无办法之下始诉诸社论，而管理这些媒体的官员，也对户籍制度甚为不满，正义在这里超越了党性，终在推出后震动全国。<br />
<br />
惩罚13媒体是锯箭疗法<br />
<br />
消除户籍制弊端才是正途<br />
<br />
然而遗憾的是，中共当局对共同社论却不是包涵理解并据此以察民困，据本报记者了解，这13家媒体在共同社论刊行后，都受到几乎如出一辙的对待──有关传媒的负责人及其所属的省委宣传部都被训话，部分更收到上级严令s若在两会期间采访及报道户籍制度新闻，立即会被查办，记者更会革职。两会开始了几天，只有温家宝总理在《政府工作报告》提过一下，其它有关户籍制度的问题鲜有人提，报道自然是少之又少。这篇令人曾经期待崛起的大国从物质富裕过渡到文明富裕的社论，就在大棍子挥舞下，消失在960万平方公里的国土之上。<br />
<br />
从中共至今的处理手法来说，尚未看到如何定性共同社论事件，我们认为，这篇社论有其客观社会背景，当中共自胡锦涛以下「构建和谐社会」几乎天天都挂在嘴边，社论呼吁的正正就是和谐及包容。改革开放30年，内地经济发展各地不同，城乡之间的差距不小，但总的来说，中国与世界的差距在缩小，内地各省之间也愈加拉近，要求彻底消除带?强烈歧视意味的户籍制度，是「和谐社会」和「开放包容」这两个目标的彰显。<br />
<br />
不过，中共的封杀式处理让人心中有气――于全面掌控国家机器的中共而言，把13个媒体的总编辑一夜之间全部换走只是闲话一句，但这能令人心悦诚服么？难道中共会认为，捂住了这些媒体的嘴巴便能把户籍制度的弊端消除？中共如今抡起大棒子打将下来，不免令海内外觉得，原来今天的中国还说不上是以民为本，广开言路的目标更是八竿子打不?。<br />
