																											<div id="postmessage_506261" class="t_msgfont">总理号召监督政府 敢言媒体突遭整肃（图）<br />
2010-03-05<br />
<br />
中国今年人大会议周五开幕，总理温家宝在政府工作报告称，创造条件让人民批评政府、监督政府。但当局被指言行不一。据报，中宣部刚下达通知，对媒体一系列议题设立禁区，而日前发起的、13家媒体“共同社论”的北京《经济观察报》社长兼总编辑遭严重警告，副总编辑受记过处分，而负责起草该份社论的副主编则被解雇。自由亚洲电台特约记者乔龙的采访报道<br />
<br />
图片：中国全国人大会议周五在人民大会堂召开，天安门广场到处是武警。（网友提供/记者乔龙）<br />
<br />
<br />
大会上午九点在北京人民大会堂举行，温家宝在政府工作报告中，提出今年要做好八方面的工作，包括继续实施积极的财政政策和适度宽松的货币政策，保持经济平稳较快发展，坚持解决“三农”问题是全部工作的重中之重，扩大就业，改革资源分配制度，促进社会和谐进步。温家宝在最后部分特别提到：“各级领导干部特别是高级干部要坚决执行中央关于报告个人经济和财产，包括收入、住房、投资，以及配偶子女从业等重大事项的规定，并自觉接受纪检部门的监督。”温家宝还表示，要加强问责和接受人民监督：“我们要创造条件让人民批评政府、监督政府，同时充分发挥新闻舆论的监督作用，让权力在阳光下运行。”<br />
 <br />
北京大学经济学教授夏业良表示：“这个说法也都是老调重弹了，也都讲了多年，然后总是获一阵阵的掌声。但是，总是没有见到结果，如果能够这样做的话，老百姓为什么还有那么多的不满？现在说的一套，做的一套，感觉差距太大了。”<br />
 <br />
温家宝的话更引起网民的感慨和回应，网易网友发自宁波的帖子称，请问温总理:公民网上批评,举报地方官员,怎样才能不被隔省追捕?不被以诽谤罪追究?中央有措施吗? 另一名成都的网友称，先保障媒体采访和记者的权利，当事公职人员有责任回答记者提问，回避和不接受采访者，最低限度都要予以“行政不作为降级或扣除薪金处分”，还要严厉惩治。<br />
 <br />
在温家宝提出“让人们批评、监督政府”的同时，当局对媒体却再设禁区。据香港《明报》周五报道，高层已下令，凡涉及北京的负面新闻一律不准报道，媒体报道外地的负面新闻也只能转载《新华社》稿件；同时，当局还要求各家网站禁止转载对退伍军人的上访维权、部分工行职员上访、北京市拆迁补偿等事项。北京打工子弟在京升学问题的负面新闻也被划入禁令范围，媒体及其网站对城乡结合部拆迁导致农民工子弟失学的内容，只能转发政府解决此事的正面新闻，只能说好，不能有任何异议。另外，媒体不准炒作广西烟草局局长“性爱日记”新闻；甚至连当前北京市小学生过早放学，下课后无人照顾，学校为补课教师加工资一事也不准报道。<br />
 <br />
 <br />
夏业良教授曾在2005年3月主持中央电视台的“两会”点评，2009年主持湖南卫视的“两会”点评。今年他来到了一个民间网站“酷六”，进行现场点评。他说：“大家今天对这个报告应该说是有很多人感到失望，人家问到我意见的时候，我说连失望都没有，因为我一开始就没有希望，从报告的文字，从表述上来讲，跟去年非常雷同，我只能说中央采取了连续性一贯性的政策，做的跟以前差不多。”<br />
 <br />
 <br />
温家宝在报告中说，会放宽中小城市的落户条件，解决农民工的就业及生活问题。而就在在人大开幕前，中国十三份报章共同呼吁改革不平等的户籍制度，不过，据《明报》称，在中宣部强大压力下，发起“共同社论”的北京《经济观察报》高层受到整肃，社长兼总编辑刘坚遭严重警告，一名副总编辑则受记过处分，而负责起草该份社论的“经济观察网”副主编则被炒。<br />
 <br />
本台致电该报，但电话无人接听，北京一名采访两会的记者告诉本台：“听说是这样，我也听说了。”<br />
 <br />
熟悉内情的夏业良称：“其实来源还是周永康（中共中央政治局常委）的一个讲话（内容），大家以为这是户籍改革的一个重要依据，所以媒体这样做，结果没想到中央里面也有不一致的声音，包括这次报告里面，只是淡淡的提了一下。”<br />
 <br />
夏业良称，十年前已传出将来废除户籍制改为户籍登记制。“比如说，农民工在城市里面居住了八年，十年，他就是北京的居民，这个事情谈了很多年来，到现在都没有（落实）。这说明我们虽然唱了很多年的高调，但实际上没有把平等的国民待遇给农民，好像农民生下来跟城市居民就是不平等的。”<br />
 <br />
温家宝的报告在谈到教育体制改革时称，创建若干一流大学，培养杰出人才，中央财政要加大对中西部高等教育发展的支持。对此，北京理工大学教授胡星斗认为：“要建一流大学，关键在于要建立现代大学制度，大学要去行政化，要实现政校分开，要实行教授治校，学术自由等等，只有这些做到了，才有可能建成世界一流的大学。”<br />
 <br />
夏教授更提出质疑：“中国的教育体制改革谈了多年，1998年5月份搞了一个‘985行动计划’。当时在全国宣传得很厉害，说是用15年时间，国家大力投入北大和清华，把北大清华变成一流大学，现在只剩三年时间了，他肯定不能自动变成世界一流大学，这个问题还没有解决，现在又说让若干所大学成为世界一流大学，也没有说哪一年实现。”<br />
