													<h2>户籍改革　维稳与维权之争</h2>
						
						
																											<div id="postmessage_504632" class="t_msgfont">Apple Daily 苹果日报网上版 －－2010-03-05<br />
户籍改革　维稳与维权之争<br />
<br />
经过个多月来网上热议，全国十一个省市十三家报纸选定在全国两会前发表共同社论，主张人民应平等享有宪法所保障的就业、医疗、养老、教育、迁徙等公民权利，但由于户籍制度歧视农户，滋生腐败，民怨四起，因此呼吁政府逐步以人口信息登记制度取代二元户籍制度，最终消除自一九五八年《户口登记条例》以来的一大弊政（原是毛泽东发动大跃进运动的重要工具）。<br />
<br />
制造不平等怨声载道<br />
<br />
先回顾一下目前中国的户籍制度。此一制度产生了两类不平等现象：一是农民与城市居民地位不平等；二是本城市居民与其它城市居民地位不平等。根据现制，上述两类「外来人口」应申领暂住证，接受监督检查。夫妻分居两地，父母子女难聚。没有本市户籍者难在本市找到工作。本地院校也往往优先招录本地学生。非本地家长更要为学童向学校支付高昂的借读费或赞助费。养老保险和医疗福利都没有提供给外来人口。非本地中学生参加高考，非本地人民办理护照和通行证，都必须返回原籍地。更重要的是，这两类外来人口往往在城市中为数众多，民众心有不平，厌恶分级分化，面对贪污腐败，钱权后门大开，当然怨声载道，隐伏社会危机。<br />
<br />
当局知道问题严重，在五年前启动户籍改革，但进度缓慢，并出现两种不同意见。<br />
<br />
一是从「维稳」的管治角度看待户籍问题。中央政治局常委周永康在上月中旬《求是》刊文指出：为了「巩固党的执政地位，维护国家长治久安」，就要推进户籍管理制度改革，探索「以证管人、以房管人、以业管人」的流动人口服务管理新模式。有人猜测，这一说法意味着今后只要在城市有职业，有住房，就可取得户籍。这种类似当年上海「蓝印户口」的改革方向，不会改变目前国内户籍制度管民牧民、限制民权的本质，只是放宽了取得特权（城市户籍）的资格，产生更多特权人士，滋生更多贪腐苗头。况且，多数外来人口根本没钱置产，工作也不稳定，又凭甚么来「转正」？歧视完户籍后，还要歧视贫穷，民怨当然难消。<br />
<br />
莫以维稳抗历史潮流<br />
<br />
另一种意见是从「维权」与自由、民主、平等的宪政角度出发，要求逐步引入人口信息登记制度，先在中小城市试点，定下时间表和路线图，逐步推展到全国城市，终极取消户籍制度；至于农地制度下农民身份和社保资格的确认或转换，完全可以分开处理，平行改革，不宜混淆。这才是「特权退一分，民权进一分」的循序渐进思路，原则上值得支持。以目前国内的经济情势来看，沿海地区已经出现「民工荒」现象，农民工回流内陆城市就业，想要「蜗居」沿海地区的农民工压力减少，已不致构成市政设施或社会秩序的庞大压力，因此启动户籍改革，正逢其时，掌握时势，刻不容缓，万莫再以「维稳」危言，妄图抗逆历史潮流。<br />
<br />
桑普<br />
