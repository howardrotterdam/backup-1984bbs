													<h2>《经济观察网》原副总编谈撰文户籍制度联合社论内情</h2>
						
						
																											<div id="postmessage_510160" class="t_msgfont">我是温和的建言者<br />
<br />
华尔街日报&nbsp;&nbsp;2010/03/09 18:20:59<br />
<br />
以下是《经济观察网》前任副总编辑张宏的信件全文。<br />
<br />
3月1日13份报纸共同发表《提请两会代表委员敦促加快户籍改革》的社论之后，引起了挺大的反响，各方也对此共同社论的出台背景有许多猜测。作为当事人，我觉得有必要在适当的时机与可能的媒体上发布这件事情的来龙去脉。有评论称此事件当载入新闻史，我个人觉得可能意义不会那么大，但本着为读者负责的态度，我愿意撰文说明。<br />
<br />
共同社论这种形式的设想源于《经济观察报》去年与英国《卫报》一起发表关于应对全球气候变化的56家媒体共同社论。当时我负责与《卫报》联络、探讨与翻译了共同社论，对整个操作过程了解比较深入。此后便萌发出我们在国内是否可以也以相同形式发表社论的想法。<br />
<br />
以户籍作为切入点，这是我的另一位同事的主意。选取此为议题，主要考虑到的是户籍制度的改革已经在多点突破，许多城市正在加快推进，而包括温家宝总理在内的中央政府高层官员对此项改革也有过多次公开场合的表态。我们认为就此议题发表社论是符合中国政府改革方向、符合广大民众利益，风险应该不大。有一些外电称此举可能来自政府高层授意，其实事情绝非如此。这只是几个小编辑闭门造车的结果，只不过引起的动静超过了我们当初的预料。<br />
<br />
此外，我们选取两会作为发表此社论的时间窗口，表明的是媒体参与中国整体改革的一种期望。坦率地讲，我活了36岁，从不知道哪位代表委员是我选取出来的，会为我申张。我想许多中国的民众也会有类似的看法。作为媒体，我们期望大众的声音可以让这些“代表民意”的代表委员们听到。这是一种温和的态度，但也是一种以前少见的媒体的直接表达。<br />
<br />
整个户籍改革策划案实际上有四个步骤，而共同社论是其中的点睛之作。<br />
<br />
第一步是在1月26日经济观察网上挂出户籍制度改革的投票倡议、征文与相关专题，同时邀请两家网站共同参与。共同社论的首段“中国患户籍制度之苦久矣！我们崇信人生而自由，人生而拥有自由迁徙之权利！”字样，最初即出现于我所起草的网站投票倡议书中。我们的投票倡议获得了读者踊跃的参与，参与投票人数达到3500人以上，对经济观察网这样规模的网站实属难得。<br />
<br />
第二步是在2月22日当期报纸中推出名为“愤怒的户口”的户籍专题。这个专题主要是采写因户籍制度而面临过现实困难的人群的遭遇，并约请专家进行访谈。这个专题已经开始引起一些反响。<br />
<br />
第三步是高潮，在两会期间3月1日当期推出共同社论。由于春节因素，我们约请其他媒体共同参与的工作受到了一些影响。我们本来预期是有20家以上的媒体可以参与，但实际上参与家数比我们预想要少。此篇社论的初稿是由我的另一位同事撰写，于2月7日写出初稿发给我修改。我做了很大幅度的修改，后来见报的文字基本上就是此稿。在2月9日发给我的同事后，他就宪法规定内容又提出修改意见，而后我们又根据其他报社的反馈做了一些字句上的小幅修改。我承认此篇社论文字激越，但这也是我一直信奉的风格，评论必须一针见血。报纸出版后，由于我们约定的共同发布时间是3月1日，所以向各大网站发布的社论文稿到3月1日早上才放行，而经济观察网也以头条发布此篇共同社论。社论发布后，遂成燎原之势。<br />
<br />
第四步是收尾。按照我们的计划，在共同社论发表后，我们至少会写两篇文章，一是自我对此共同社论进行报道，二是再揭密整个策划案的过程。我本人在下午时先写出一篇《媒体不只是见证者：我们为什么表发共同社论》（此标题因编辑失误出现错误，应为“发表”而非“表发”）的评论文章，在网上发表。同时我们也发布了一篇《13家媒体发布户籍改革共同社论引发热议》的报道文章。而原计划中的揭密策划案过程的文章，因故没能执行，此为此次策划案中惟一的缺憾。<br />
<br />
在共同社论发表后，引起的反响可说是远远超出我们当初的预料。我们预料过会有一些反响，但没想到会如此之大。正应了那句古话“世无英雄，乃使竖子成名”。我不敢贪天之功，也不愿诿过于人，所以在此篇文章中将所有参与媒体与个人的名字一概隐去，只剩我这一无牵挂之人的名字。事实上，每位读者都明白，共同社论引起的广泛影响，并不是媒体的力量有多大，而是民众的期盼有多么热切、焦急！<br />
<br />
在这件事出来后，我本人获得了相应的处罚，其他同事和合作媒体也受到连累。想及此，颇有负疚之感。这不能归咎于报社，因为面对的是不可抗力，我们在做事时总要考虑到还有许多人的饭碗应该保全。在此我要感谢与我一起做出努力的同仁们。<br />
<br />
我的父辈因户籍制度受过许多苦，我的许多朋友甚至下一代现在还在因此制度而受苦，疲于奔命，欲诉无门。我不是专家，提不出完整充分的改革方案，但我有一种坚定的信念，一项无视人的尊严与自由的法规，终究要被历史扫入垃圾筐中。我期盼着这项制度的最终消亡，届时相信会有许多人喜极而泣，奔走相告。作为一位媒体人，我只是在尽我的责任与义务，而我们的每一个人也都应该承担起相应的责任与义务。<br />
<br />
我是一位温和的建言者，无意挑起巨大的波澜，只不过事态的发展超出了预想。最后请大家记住，我现在是一位独立的评论人。只是希望这些文字能让大家对整个事件有全面的了解。谢谢大家对我的反馈，不论是支持，还是批评。</div>

							
							
							
							
