社论援引中国《宪法》条款，称迁徙自由式人权和人身自由不可分割的部分，是“宪法赋予国民的基本权利。”而现行的户籍政策却事实上造成了城市居民与农民、城市居民之间地位的不平等，制约了中国公民的自由迁徙，明显与《宪法》相违背。<br />
<br />
社论刊发后，被中国网络媒体广泛转载，并引发网民广泛反响和讨论，但如《华尔街日报》说的，到了周二，这篇社论在许多网站上已经消失得“无影无踪”了。<br />
<br />
据香港《明报》前往北京采访两会的采访组报道，中共中宣部大举封杀内地13家媒体促请户籍制度改革的“共同社论”，负责网络新闻管制的国新办网络局下令，“关于13家媒体推进户籍改革内容，新闻环节、论坛、博客等环节请尽快删除”。《明报》引“知情人”称，删除该条社论网稿内容直接负责人为国新办一名级别不低的官员。<br />
<br />
更有部分传媒收到文件通知，禁止传媒在两会期间采访报道有关户籍新闻，记者如违反的话，将马上被“炒鱿鱼”，而刊发这一共同社论的相关报社及其所属的省委宣传部门都被训话。<br />
<br />
《经济观察报》的编辑张宏在题为《媒体不只是见证者：为什么我们要表发共同社论》透露，这一社论的写作，是受到英国《卫报》在哥本哈根气候会议期间发起的全球56家媒体共同社论的启发。<br />
<br />
张宏又写道，“共同社论”表达出国内媒体关注并期望加速推进户籍制度改革的强烈声音，表现出国内媒体参与并推进中国改革进程的强烈愿望。他说，毫无疑问，媒体所能做的事情就是“鼓与呼”，手握权柄的大人物才是最后的拍板决策者。<br />
<br />
张宏呼吁，“作为媒体，我们将这样的倡言以共同社论的形式向两会代表委员建言；而作为个人，你们对此共同社论的评论（不论是赞成还是反对）、转贴也都将推动整个中国社会变得更加民主、开放和以人为本。”<br />
<br />
但似乎，虽然关于户籍改革，官方已经有所松动和表态，但媒体管制部门并不乐见这种“鼓与呼”。<br />
<br />
最新的发展是，据香港《明报》报道，在中宣部的推动下，发起13家中国报社“共同社论”的北京《经济观察报》高层受到整肃，社长兼总编辑刘坚遭严重警告，一名副总编辑则受记过处分，而负责起草该篇社论的”经济观察网”副主编则被“炒鱿鱼”，丢掉工作。<br />
<br />
而为了营造两会期间的“和谐气氛”，中宣部下令划定两会报道的禁区，除了不准报道13家媒体的”共同社论”外，凡涉及北京的负面新闻也一律不准报道，媒体报道外地的负面新闻也只能转载新华社稿件.<br />
<br />
同时，当局还具求要求各家网站禁止转载对退伍军人的上访维权、部分工行职员上访、企业退休员工待遇问题、’万里大造林”受害客户等上访内容、北京市拆迁补偿等事项。同时，还对北京打工子弟在京升学问题的负面新闻也被划入禁令范围。<br />
<br />
不仅如此，当局还要求内地媒体及其网站对城乡结合部拆迁导致农民工子弟失学的内容，各家媒体只能转发政府解决此事的正面新闻，只能说好，不能有任何异议。同时还要求内地媒体不准炒作广西烟草局局长”性爱日记”新闻。<br />
<br />
甚至连当前北京市小学生过早放学，下课后无人照顾，学校为补课教师加工资一事也不准报道。</div>

							
							
							
							
