　　　　 夜已经很深了，可窗外还有零星的鞭炮声在响起，我没法去评价这到底代表着什么。对于通钢员工，也许代表着胜利；对于吉林政府，也许象征着嘲讽；对于陈某，也许是为他送行的悲歌。<br />
　　　　<br />
　　　　 事情还要从7月22日说起。这天，从设立在吉林省长春市的通钢集团传来消息，建龙集团再次入主通钢，控有新通钢集团65%的股权，吉林省国资委仅占有34%的股权。也就是说，吉林省最大的钢铁企业将被民企建龙集团吃掉，成为民企资本的一部分。<br />
　　　　<br />
　　　　就在消息传来的当天，通钢集团的主要生产基地，也就是通钢集团的前身通化钢铁全员沸腾。空气中弥散了咒骂、惊谔、不解与沮丧。因为，这距离建龙与通钢第一次重组后的股权分立仅仅只有三个月。2005年10月，在吉林省委省政府的积极运作下，全国最大型的钢铁投资公司建龙集团与吉林省唯一一家大型国有企业通钢集团连姻。建龙以资本注入形式入主通钢，占有通钢近40%的股份。虽占有股份不是最多，但通钢集团及通化钢铁的总经理、财务主管却在很短时间内都换成了建龙人，美其名日“引入民营机制”。于是，一场所有人都看不清的运作开始了。从那时起，通钢管理体制完全走样，国不国、民不民，高层拿着高薪、口里却声声喊着代表党和国家。员工则被裁员、减薪。<br />
