　　　　<br />
　　　　在建龙与通钢股权未分立之前，仅2009年一季度，通化钢铁就亏损了近10亿元，吉林新区却仅亏损2亿左右。而在股权分立后，4月份，通化钢铁仅亏损了近 1亿；5月份，微亏；6月份，已经盈利了6000余万元，全体通钢人欢欣鼓舞。7月份，全通钢都在憋着劲要再打个漂亮仗。而恰恰在此时，建龙再次入股通钢，而这次是对通钢绝对控股。面对国有企业如此被民企收并，面对通钢人如此被建龙人玩弄，通化钢铁所有员工及家属群情激奋、怒不可竭。<br />
　　　　<br />
　　　　作为股份制国企，在企业大的问题上应该有一定的办事原则。首先，有共产党及工会组织，企业有大的变更，应及时召开职代会通报消息，并征求员工意见，因为通钢毕竟还是国企，应该按照国企工作程序推进革新；其次，员工也是通钢的股东，虽然人均持股量较少，但近2万人的员工队伍，其股金总量也是不低的，企业有变更应该召开股东大会予以提前公布。而直到两个企业决定再次重组，连通钢的很多高层都不知晓。而关于企业重组的宣传材料，在公布重组的第二天才发到员工手中。吉林省国资委关于通钢重组的决定下发后，通钢有几位总经理当即集体辞职，而通化钢铁的工人运动也全面展开。<br />
　　　　<br />
　　　　这是一场自发的，没有任何组织的工人运动。这一切源于对建龙的憎恶，源于对吉林省高层领导决策的不满。7月23日上午，吉林省国资委部分领导、建龙集团部分高管到通化钢铁召开重组大会。遭到近50名员工家属、100名在职员工的包围冲击。当天下午，通化市公安部门开始向通化钢铁各单位质询具体情况。7月 24日上午，近3000名在职员工及员工家属在通化钢铁办公大楼前集会，高举“建龙滚出通钢”等标语，高喊口号进行示威集会。上午9时左右，示威人群向通化钢铁冶金区进发，并对1、2、3号高炉铁路运输线进行封堵。不久，通化钢铁1、2、3号高炉先后修风停产。中午，示威人群没人散去，而是集中在冶金区各铁路沿线，至使4、5、6号高炉先后修风停产。直至下午，通化钢铁7号高炉修风停产。至此，通化钢铁整体生产停滞，各主体、辅助单位陆续减产、停机。<br />
　　　　<br />
　　　　 7月24日当天，在通钢宾馆某房间内，刚上任的通钢集团总经理、建龙人李某坐镇，分别与通化钢铁各分厂主管领导谈话。通化钢铁各主管经理分别下到各分厂与中层干部及职工代表对话，要求做好员工队伍稳定工作。而刚刚上任的通化钢铁总经理、建龙人陈某带队到焦化厂与中层干部及职工代表对话。此时，不知何故，部分示威人群得知此事，并迅速聚集到焦化厂，对召开会议的办公楼进行包围。据说当时陈某与示威人员发生了口角冲突，并激怒了示威人群，一群示威人员冲入会议室，把陈某拖进走廊进行群殴。消息传开后，各区域示威人群大量向焦化厂集中。此期间，通化市调集了大量警力，由公安、交警、武警组成的大批治安人员进驻通钢冶金区，对通钢冶金区进行封锁管控，吉林省及通化市相关领导陆续赶到现场，但依然无法控制局势。至晚19时，通钢冶金区已聚集近万人。据说，当时陈某已被群殴多次，并有说社会人员参与了对其殴斗，陈某已经死亡，死状凄惨。但大量员工依然聚集，不允许政府部门人员进入。晚21时左右，通化钢铁通过电视台向通钢员工及家属发布公告，宣称省政府决定建龙退出，永不再参与通钢重组。晚22时左右，示威及围观人群才纷纷散去。<br />
　　　　<br />
　　　　事情发生的太突然，而且事态规模太庞大，出乎所有人的想像。直至零晨，我依然不敢相信发生的一切。陈某我见过，一个公开场合讲话都会手脚发抖的职业经理人。我所知，通钢没人评价他是个合格的总经理，但有人满意，因为他听话。应该说，他的结局是个悲哀，即便他有再多的过失，也不应该付出生命的代价。他的死是吉林钢铁企业改制失败的恶果，是吉林省高层领导的严重工作失误，是建龙人投机倒把的罪有应得。通钢人，一群血性的汉子，面对自己祖辈用50余年建立起来的基业，无法释怀一切会被别人收并，特别是在企业正在走出困境，全面谋求发展的时刻，在得不到上级支持与理解的时候，采取了极端的手段。建龙集团，一群资本运作的高手，一个有着深厚背景的企业，但他们作梦也不会想到，这次跟斗栽大了。东北人真的勇武到这种程度，矛盾真的能发展到如此不可调和。通钢领导，除几名实干派之外，一群泛泛之辈，而且官本位思想、利已主义思想严重，心中无大局无大志，有的只是眼中的铜臭及私利。<br />
　　　　<br />
　　　　 事件的结局是个悲剧，虽然窗外还响着鞭炮声，但在我听着，那却是来自地狱的嘲笑声。我在深深的悲怆中看着时针，那是对吉林省高层急功近利的悲怆、对社会发展畸变的悲怆、对通钢人粗蛮盲目的悲怆、对建龙人无尽贪欲的悲怆、对死者家属痛失亲人的悲怆……<br />
　　　　<br />
　　　　由此，我联想起了几件吉林省发生的事。长春市委副书记米凤君腐败案件、吉粮高管腐败案件、松原市高考舞弊案、通化市公安局涉黑案、吉林钢铁企业体制改革失败、吉林人参产业衰退……这一切说明了什么？是吉林病了，而且病的很重。作为一农粮大省，吉林省应该是一个培育质朴和真诚的基地，而不应该是一块滋生贪婪与利欲的土壤。通钢的问题所反映的不应该仅仅是国有企业改制中的矛盾问题，而更应该是深层次的东西。难道非要以人命为代价才能换来关注吗？到底谁能来救救吉林？<br />
