													<h2>中组部回应共产党信仰危机质疑：入其他党动机同样不纯</h2>
						
						
																											<div id="postmessage_714538" class="t_msgfont">中组部回应信仰危机质疑：部分人入党动机不纯<br />
2010年07月01日01:54新京报杨华云 等我要评论(20) 字号：T|T <br />
<br />
<br />
11位新闻发言人昨日首次集体亮相，中组部新闻发言人邓声明答问。本报记者 王贵彬 摄<br />
<br />
<br />
本报讯 中国共产党建党89周年纪念日前夕，中共中央11个部门的新闻发言人集中面对媒体。外宣办负责人表示，今后发言人还会越来越多。<br />
<br />
更多发言人将亮相<br />
<br />
这11位发言人昨日在中央外宣办新闻发布厅集中亮相，中央外宣办主任王晨当上了此次记者见面会的主持人，依次向媒体介绍了出席见面会的11位发言人。<br />
<br />
王晨表示，中共十七届四中全会首次明确提出建立党委新闻发言人制度，中央外宣办作为推进党委新闻发言人制度建设工作的主要职能部门，将进一步推进党务公开，满足社会公众对党务信息的需求。<br />
<br />
此次亮相的11位发言人，分别来自中纪委、中组部、中宣部、中央统战部、中联部、中央台办、中央外宣办、中央党校、中央文献研究室、中央党史研究室和中央档案馆。据了解，这些发言人都出生于上世纪五六十年代，年纪最大的58岁，最小的45岁。<br />
<br />
目前中共中央共有26个部门，已有11个部门设立了新闻发言人。王晨接受本报记者采访时透露，将会继续推动其他中央部门设立新闻发言人，“今后会有更多的中央党务部门发言人出现”。<br />
<br />
不回避敏感问题<br />
<br />
在见面会上，近1个小时的时间内，新闻发言人共回答十余个问题。这其中，有如何治理腐败高发领域的问题，有如何评价“新闻自由”的问题，还有对华南地区罢工风波的新闻管理问题。<br />
<br />
此外，中纪委新闻发言人在回答记者提问时披露，截至5月底，全国排查了34万个工程项目，其中发现不同程度问题的14万个；去年8月份以来共查处工程建设领域中的案件9900多件，党纪政纪处分5800多人，其中移送司法机关3400多人。<br />
<br />
■ 焦点<br />
<br />
【信仰】多少人因纯粹信仰而入党？<br />
<br />
美国有线电视新闻网记者提问：中共党员到底有多少人是因为纯粹的信仰而入党的？中国共产党如何应对信仰危机？<br />
<br />
中央组织部新闻发言人邓声明：“我们的党员99%以上都在生产、工作、经营管理一线，都是普通劳动者，他们加入中国共产党主要是拥护党的主张，信仰党的理论，认同党的宗旨，追求党的先进性。”<br />
<br />
他亦坦言中国存在有人“入党动机不端正”的情况，也不回避党员队伍中“不乏腐败分子”的事实，他表示，一方面要对提出入党申请的人进行“相当长时间的考验”，一方面也要在其入党后“加强教育”，同时还要完善“清理、处置不合格党员的退出机制”。<br />
<br />
【培训】私企老板自费读中央党校？<br />
<br />
香港文汇报记者提问：私企老板自费入读中央党校，舆论认为有违党校宗旨。<br />
<br />
中央党校发言人罗宗毅：这是个“敏感的问题”，“2008年《中国共产党党校工作条例》颁布后，党校就不再举办民营企业家学习班。”不过，“民营企业家在改革开放和社会主义现代化的进程中发挥了很大作用，但确实有一些民营企业家的素质相对滞后，需要加强学习”。<br />
<br />
在谈到中央党校为县委书记举办培训班的问题时，罗宗毅又用“上边千条线、下边一根针”、“麻雀虽小、五脏俱全”以及“郡县治、天下安”等形象的说法，说明县级党委和政府是中国共产党“治国理政”、“直面百姓”的重要力量。<br />
<br />
■ 花絮<br />
<br />
一块背景板的“变迁”<br />
<br />
对于昨天的见面会会场，记者们并不陌生――过去几年国务院新闻办的发布会都在这里举行。不过，中央党校常务副校长李景田前日在这里发布党校培训信息时，背景板上写的“国务院新闻办公室”字样，已变为“中共中央对外宣传办公室”。<br />
<br />
对这个发布会名义的变化，中央外宣办新闻发言人郭卫民表示，这个变化是推进党务公开工作的一个做法，今后有关党务信息的发布，包括请党的部门领导出席发布会，都会以中共中央对外宣传办公室的名义。而有关政务信息发布，包括政府部门的发言人出席发布会，还会以国务院新闻办公室的名义举行。<br />
<br />
11位发言人脱稿畅聊<br />
<br />
11位发言人中，既有如外宣办发言人郭卫民、中台办发言人范丽青这样和媒体打交道经验丰富的，也有像中央党史研究室发言人张树军、中央文献研究室发言人闫建琪这样与媒体接触并不多的。但在介绍本部门情况和新闻发布工作时，这11位发言人均能脱稿。<br />
<br />
在回答提问时，这些发言人也均为脱稿，即便面对诸如“信仰”、“党校培训民营老板”这种具有挑战性的问题时，这些发言人也都显示了之前的准备工作和对本部门工作的熟悉。<br />
<br />
观点<br />
<br />
设立新闻发言人、建立新闻发布制度，这表明中央是很诚恳、坦然、郑重、负责地来推动这项工作，而不是敷衍、应付媒体、做表面文章……新闻发言人的级别高，决定了其讲话的权威性，而熟悉相关领域工作，使得其能够清晰、准确地发布信息，避免过往一些新闻发布中个别官员因不了解具体工作而讲“外交辞令”、“空话”、“套话”等现象。<br />
<br />
――中央党校教授叶笃初<br />
<br />
由于中国政治体制具有独特性和不可复制性，中国共产党作为执政党，其政党行为在很大程度上影响着政府的执政行为，这就决定了在党内建立新闻发布制度、开展党务公开“非常重要也非常必要”。“新闻发言人制度是一种较为直接而有效的表达方式，可以让老百姓更直接地了解党的执政理念、执政方式和执政行为。”<br />
<br />
――中国社会科学院新闻研究所所长尹韵公 据新华社<br />
<br />
同问同答<br />
<br />
昨日上午，中央11部门新闻发言人集体亮相。发布会后，本报记者对其中5位发言人进行了采访。<br />
<br />
5位发言人对如何面对媒体，如何面对尖锐问题，如何理解肩头责任分别给予回答，坦诚、亲切、没有架子，是记者对他们的印象。<br />
<br />
问题1 如何做好党务发言人<br />
<br />
邓声明（中组部新闻发言人）：组织部门发言人的责任，是把组织工作的理念、工作的重点告诉大家。<br />
<br />
2008年开始，我们就在搞组织工作满意度调查，全国有8万人参加，涉及所有行业和地区，每一个省和县都有人参加。<br />
<br />
让人民群众给组织工作提意见，让人民群众来评判组织工作，让组织工作建立科学的评价机制、畅通的民意表达机制、有效的压力传递机制、改进工作的动力机制，让组织工作能问计于民、问政于民。<br />
<br />
张树军（中央党史研究室新闻发言人）：刚才在台上，我准备说一句，对我们（中共中央党史研究室）来讲，只是刚刚起步，缺乏经验。<br />
<br />
我们的责任是把党中央规定的我们的职责、任务，做了哪些工作，社会上关注的热点是什么，尽量传达给公众。<br />
<br />
李军（中央对外联络部新闻发言人）：就对外联络部而言，一是要向国内媒体说明我们党的对外交往情况，二是要向国际社会说明我们党。很重要，也很复杂。<br />
<br />
张献生（中央统战部新闻发言人）：发言人责任确实很重，统战部门相关的方针政策、工作进展、理论研究成果、重要信息，都需要通过我们来准确地向社会发布，我们不能出差错，不能因为我们发言的不恰当，给党的事业造成损失。<br />
<br />
闫建琪（中央文献研究室新闻发言人）：新闻发言人要正确地反映单位的工作情况、工作思路，甚至工作状态，毕竟我们代表一个单位，而不是个人的见解，反映一个单位的工作水平，责任很重。<br />
<br />
问题2 如何避免说官话套话<br />
<br />
邓声明（中组部新闻发言人）：我自己还在学习之中，没准儿我就会说错话，希望大家能够谅解。<br />
<br />
张树军（中央党史研究室新闻发言人）：应该是第一次面对这么多的媒体，我自己也有些感冒了，再加上我这个人爱出汗，他们都不擦汗，就我一人擦汗（笑）。有点紧张。<br />
<br />
我们搞历史最讲究实事求是，我们党史坚持实事求是的原则，对我们党历史上作出的贡献要充分肯定，对我们走过的曲折我们也不回避。重在分析出现曲折的原因，我们能够接受教训，我们党的历史也是这么走过来的。<br />
<br />
刚才提到“文革”，如果没有“文革”，我们的改革开放就很难在那个时代启动起来，因为它的教训太深刻，教训也是一笔财富。<br />
<br />
李军（中央对外联络部新闻发言人）：实事求是就会避免官话和套话。对于今天的表现，可能回答得太长了（笑），做发言人还是要简明扼要，至于流畅上，我之前是做翻译出身。媒体是朋友，增加老百姓的理解要通过媒体，千万不能得罪（笑），我们一定要提供好服务。<br />
<br />
闫建琪（中央文献研究室新闻发言人）：（避免官话套话）其实很简单，就是坦率地交流，做了什么工作，就老老实实地告诉大家，实事求是地反映工作状态。<br />
<br />
今天略微有些紧张。自己也看到和其他发言人的差距。主要还是我们准备得不够。<br />
<br />
我觉得发言人和媒体间还是要相互信任，我们是老老实实地、实事求是地讲我们自己的情况，我们也相信媒体会实事求是地反映我们的见解和意见。<br />
<br />
（会定期开发布会吗？）有了研究成果肯定要发布，但是要定期发布可能有一定的困难，比如成果没有出来，怎么发布呢？但我们的大门是敞开的，大家可能都觉得文献研究室很神秘，实际上我们的工作会研究大量的中央领导人的手稿、文稿和文件，但是我们工作的任务和性质是透明的。<br />
<br />
问题3 是否会说“无可奉告”<br />
<br />
邓声明（中组部新闻发言人）：你看，我考虑的问题有一本，这只是其中一个（发布会上，有记者提问关于入党是否出于信仰的问题）。<br />
<br />
前天我们在发布党员数字的时候，我看到外电就有一个报道，说的是我们党员有的是为了理想和信念，有的是为了其他的想法。其实这个事情并不是今天才提，也不是这个时代才有某些党员入党是有动机的问题，各个党也都会存在。<br />
<br />
中国共产党对党员的入党要求是很严格的，有考察期，有教育期，入了党还有预备期，也许你们不知道，光是去年取消预备党员资格的就有5000人，这些都是对他们的考验和观察。<br />
<br />
（是否会说“无可奉告”？）比如有些事情我确实是不了解的，我也只能告诉大家我确实不知道。<br />
<br />
张树军（中央党史研究室新闻发言人）：“无可奉告”这四个字以后不会从我的嘴里说出来，但是我也会根据一些情况，实事求是地来回答一些问题。<br />
<br />
李军（中央对外联络部新闻发言人）：（无可奉告）尽量少说吧。比如说我确实不知道的，那只能说我不知道，这个事情我无法说。有些是双方约定的事情，比如有双方交往中约定的事情，要遵守约定，那确实不能说。<br />
<br />
张献生（中央统战部新闻发言人）：这要看是什么情况，有些是我们职责范围之内，我们会全部回答；有些情况，我们确实不太清楚；有些需要有关部门作出专门回答的，这就需要有关部门来说。<br />
<br />
闫建琪（中央文献研究室新闻发言人）：不知道就是不知道嘛，老老实实地说。如果对于某个事情我说不是很清楚，我想你们也会理解的。</div>

							
							
							
							
