													<h2>郎咸平：沃尔玛天天低价背后是剥削消费者</h2>
						
						
																											<div id="postmessage_208067" class="t_msgfont">　2009-08-17 00:09:54 来源：郎咸平博客 <br />
<br />
<br />
在今年6月份的一个零售业论坛上，有一位说道：其实沃尔玛在中国目前的收购行动没有我们想像的那么大，它收购的步伐也是比较慢的，所以我比较放心。另一个大连的企业家说：我根本就没有把沃尔玛当成对手，虽然沃尔玛已经进入大连，甚至开在我附近，可是我们的业绩还是非常的好，我们还是透过一样的销售维持着我们的市场份额。因此沃尔玛进来，不会对我们造成任何的冲击。<br />
<br />
　　沃尔玛的三大法宝<br />
<br />
　　1962年成立的沃尔玛公司，到今天为止在全球16个国家开设了7800多家商场，成为全球最大的连锁零售商。1996年沃尔玛在中国开设第一家沃尔玛购物广场和山姆会员商店，13年之后的今天，沃尔玛在全国89个城市开设了146家商店，这一数字超过了家乐福的136家，同时拥有好又多35%的股权和102家门店。在今年一月，沃尔玛就开了17家商店，几乎和08年全年20家的数量追平，而这些店大部分开在二三线城市。家乐福也表示今年要开28家分店，超过去年的22家，其中一大部分也开在二三线城市。<br />
<br />
　　让人惶恐的是，他们不是在开店，而是在做在整个产业链的整合。在06年，他们的自有品牌有22.5%，08年是10%，到2010年他们的自有品牌要提高到20%。而自有品牌的特色就是价格非常便宜，通常能便宜到20%，有的甚至可以便宜到40%。沃尔玛除了在下游控制自己的品牌之外，在上游也把产链做的非常好。兴源公司在辽宁瓦房店建立了沃尔玛绿色水果转换有机水果直接采购基地，对当地5000亩的绿色水果进行有机转换，到2011年，沃尔玛计划在中国让100万农民参加到这个所谓的农超对接的项目中。他们也明确表示，他们将建立一条涵盖供应商、配送中心、卖场等所有环节在内的绿色供应链。<br />
<br />
最让我紧张的是沃尔玛透过我们中国农业改革进一步的收购我们农地的经营权。沃尔玛在背后进行资本运作，推动了非常多的农业公司以中国农业公司的名义大量收购了城市附近的优质农田经营权，收购之后变成集约式耕种，使这些农民为沃尔玛打工。有人认为为沃尔玛打工也没有不好的，然而，根据统计沃尔玛在美国有130万员工，却只有61万的员工享有医疗保险的援助，仅占全美员工的49%，海外的员工很难取得这种福利。<br />
<br />
　　沃尔玛的物流成本在100元的销售额当中占1.3%，凯马特是8.75%，西尔斯是5%。通过自有品牌、直采基地、极低的物流成本形成了一套完整的产业链。沃尔玛目前的战略根本不是在门店里进行杀价竞争，而是整个产业链的上中上游一起竞争，因此他的淘汰的行业不是中国的零售行业，而是所有中间环节。在产业链竞争的格局之下，中国的零售业将面临一个前年未有的大洗牌。类似的还有高盛，高盛在2008年出了3亿美元在湖南、福建一带一口气全资收购了十多家专业的养猪场，我们还发现，2004年高盛已经入股了雨润集团，2006年以20亿元把中国最大的肉食品产业双汇集团纳入囊中，在产业链终端步下了棋局，进行养猪的产业链竞争。<br />
<br />
　　“天天低价”的背后<br />
<br />
　　这是一个自由贸易的时代吗？沃尔玛与家乐福可以中国来开超市，我们却不可以到美国和法国去开超市，在法国只要是占地超过300平方米的企业都是需要法国政府来批准。这是因为零售业是一个不一般的行业，他是上挂消费者，下挂生产者的行业。如果有一天沃尔玛掌控了整条产业链之后，他们这些外资零售业有没有可能进行联合垄断，上抬消费价格剥削消费者，下压进货价格剥削生产者，把中间利润拉大，将利润合法的汇出中国，图利美国人，图利法国人呢？这就提醒我们要有一个好的法制来规范这一切，要借鉴沃尔玛在美国对于供货商的态度。根据美国的破产法规定，假设沃尔玛连续亏欠了3名供货商5000美元以上的货款，任何一个供货商都有去美国联邦破产法院宣布沃尔玛破产的权利，因此我们更要建立法制化的游戏规则。</div>

							
							
							
							
