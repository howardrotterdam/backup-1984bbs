<br />
原定6月22日由香港一家出版社出版的前总理李鹏的六四日记，在最后一刻因“版权问题”被迫喊停。据消息人士透露，出版搁 浅是因为中共高层的政治压力。<br />
<br />
计划出版《李鹏六四日记》的香港新世纪出版社的出版人、前中共中央总书记赵紫阳政治秘书鲍彤之子鲍朴，6月 初在接受记者采访时表示，经过几个月的验证和排查，他以及相关人士都确信李鹏日记的原稿是真确的。目前由于“版权问题”，鲍朴在蒙受巨大经济损失的情况 下，在此书出版的前3天被迫停止出版。据认为，这更加印证了李鹏日记的真实性。<br />
<br />
*消息人士：中共高层极为关注*<br />
<br />
北京一位 了解鲍朴出书过程和北京高层一些情况、要求记者不透露其姓名的消息人士说，前总理李鹏坚决不授权给鲍朴出版他的日记，认为是个政治事件，高层有批示，为此 专门成立了李鹏日记的泄密专案组，查得很严，作为一个政治案件在抓。<br />
<br />
这位消息人透露，目前李鹏本人成了这件事的督办者，一天几次过问此 事，询问调查得如何。不过，现在书不出了，可能稍微松口气。<br />
<br />
这位消息人士还透露，出版人鲍朴本人以及他的父亲、目前仍被中国政府软禁在北 京的鲍彤都承受了沉重的压力，鲍彤本人目前连电话都不接了。所以，鲍朴只能以不出书作为牺牲，平息事态，一来避免因此触犯香港的版权法，二来也是要保护给 他材料的中间人。<br />
<br />
*李鹏六四日记具重大史实意义*<br />
<br />
这位消息人士说，由于李鹏在中共内部的政治地位，可以随意调阅党内机密 文件和中央文献，因此，虽然他的手稿是以日记的形式，但其作为史料的价值是很大的，远远高于鲍朴的香港新世纪出版社去年出版的、曾引起轰动的赵紫阳口述回 忆录《改革历程》。而更由于李鹏日记所涉及的重大历史事件，尤其是以邓小平为首的几位中共老人背着总书记赵紫阳和常委胡启立，在邓小平家里就将政治局颠覆 了，给六四定性和下令镇压的史实，触动了中共的敏感神经，因此, 此书的出版问题非同小可。<br />
<br />
这位消息人士说，虽然鲍朴说，新世纪永远不会 出版此书，但目前至少搞清楚李鹏日记的真实性，而且网上也流传开，因此意义还是很重大的。<br />
<br />
鲍朴星期三在接受美国之音记者采访时表示，他不 愿再谈论《李鹏六四日记》的出版问题，既然现在书不出了，以后也不会出。<br />
<br />
据报导，洛杉矶民运社团通过美国西点出版社在近日内出版了《李鹏 六四日记》。鲍朴表示，他不了解洛杉矶版本李鹏日记的事情，也不愿发表评论。<br />
<br />
他说：“确实是不知道，也没有上网看。洛杉矶的情况我也不太 清楚。我没有感觉，我没有任何感觉，也不会那什么，现在不会有感觉，以后也不会有感觉。”<br />
<br />
李鹏日记透露了当年中南海高层对如何处理八九民 运存在明显分歧，1989年5月20日凌晨北京实施戒严和调动军队进入北京，是邓小平5月17日拍板决定，除了赵紫阳，所有人都支持邓小平的决定。记者采 访的消息人士认为，李鹏在表明自己的政治正确，认为他自己在六四事件中的作为正确的过程中，透露了许多关键史实。</div>

							
							
							
							
