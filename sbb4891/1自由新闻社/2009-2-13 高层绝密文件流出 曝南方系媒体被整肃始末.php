																											<div id="postmessage_30379" class="t_msgfont">促使政治局会议做出整顿媒体决定的绝密文件曝光<br />
<br />
据公安部一位副局长向博讯记者独家透露，2008年底，中共中央政治局会议竟然讨论了《南方都市报》和《南方周末》事件，会议讨论是越过了中宣部的，实属罕见。<br />
<br />
原因就是政治局讨论的是来自情报部门的“绝密情报”，根据这份来自某情报机关的情报，海外敌对势力正在利用这两份南方最敢言的报纸，对中国展开颜色革命。密级定为“绝密”的长达7页的情报称，这两份报纸在2008年使劲鼓噪普世价值，宣扬西方的价值观，对中国政府在地震救灾、西藏问题、奥运会以及处理群体事件上，处处冷嘲热讽，甚至直接唱反调，造成恶劣影响。 (博讯 boxun.com)<br />
<br />
这位愤愤不平的公安部副局长说，他之所以要把这事披露出来，是因为这份“情报”实在离谱，这份发送到正省级的情报里使用的证据既没有海外的来源，甚至都不是从国内秘密途径得到的，整个情报里所有的证据竟然都是来自网络上的公开资料。这位即将退休的副局长的级别本来不足以阅读这份情报，但由于工作关系，情报内容被处理后下发到公安部局级干部阅读。<br />
<br />
他说，情报里用来指责《南方都市报》沦落为一份反党反社会制度的证据竟然是网络“民意”，而这民意竟然都来自一个人，就是在中国臭名昭著的网络左棍司马南。<br />
<br />
司马南在网络上开设博客，以攻击普世价值和民主为己任，对国内自由主义知识分子进行诬蔑和打击，此人在去年多次写文章批评和攻击《南方都市报》和《南方周末》，然而，由于他文章不讲根据，逻辑混乱，所有看到过的也就一笑置之，甚至当成是疯狗咬两下，大家也并不介意。所以，从始至终，南方这两份报纸并没有一个知名的作者出来回应他。一度也让这位司马南很寂寞和无聊。<br />
<br />
这些情况这位局长当然清楚，也都有掌握，也有人一直告诉他们，公安部也曾经和司马南接触过，但由于担心司马南是国家安全部的人员，所以接触并不深入。这次看到这份要把南方报业往死里整的情报，这位局长震惊了，因为这份情报里引用的“证据”以及“民意”竟然都是司马南那些网络文章里的，甚至连司马南搞笑的“天下苦两南久矣”的幽默说法也上来这份情报，成为“民意”要求管理南方两份反党反华的证据。<br />
<br />
这份长达七页的情报里竟然引用了六处司马南的文章内容，而就算一般的读者也看得出司马南的文章只是发泄性质的评论文章，可是这些字句到了某部门的情报上，竟然成了“证据”和“民意”，实在让这位暴料的副局长哭笑不得。他说他都把这些情记录下来，退休后好好好写下来，不能让这种荒唐的历史失传了。<br />
<br />
据说当时的政治局会议开得很激烈，主管政法的常委支持这份情报，要求下重手对付南方两份报纸，但他的建议遭到主管宣传的常委的抵制。最后的结果是胡锦涛开口，胡锦涛说，这事应该由汪洋处理。<br />
<br />
汪洋领会胡锦涛意思，回到广州后，对两南进行了整顿，除了把敢言的江艺平副总编调离职位后，又调整了六位领导干部的职务。同时对南方最著名的“岭南大讲堂”作了调整。<br />
<br />
副局长透露，那些炮制了这份可笑的情报的人自然对这些不痛不痒的“调整”不高兴，但却无能为力。据说汪洋得到了胡的尚方宝剑，把南都事件定性为个别领导干部的思想出了问题，是点的问题，而不是面的问题，更不是什么海外反华势力的渗透（那份情报的原话），个别调整就可以了，而且即便对调整的同志，也是以“批评教育”为主。<br />
<br />
但早被吓成惊弓之鸟的中国媒体人，经过这样一次警告，已经静若寒蝉，据说《南方都市报》和《南方周末》已经元气大伤，这位副局长一直是《南方周末》的读者，他说，他看得出来，中国最敢言的两份报纸经过这次折腾，已经锐气大伤了。最后他对博讯记者说，也许，这正是北京政府在2009年这个多事之秋送给中国媒体的第一份礼物和最后一次警告。至于那份情报是否真实，谁又会在意？ [博讯首发,转载请注明出处]-</div>

							
							
							
							
