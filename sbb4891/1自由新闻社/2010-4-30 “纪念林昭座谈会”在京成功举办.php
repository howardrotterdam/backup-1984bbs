													<h2>“纪念林昭座谈会”在京成功举办</h2>
						
						
																											<div id="postmessage_590592" class="t_msgfont">“纪念林昭、时空和谐座谈会”在中国宋庄画家村成功举办<br />
<a href="http://blog.artron.net/space.php?uid=70802&amp;do=blog&amp;id=391326" target="_blank">http://blog.artron.net/space.php ... =blog&amp;id=391326</a><br />
<br />
&nbsp;&nbsp;4月29日是林昭蒙难日，在北京宋庄民办的思想研究所建立的林 昭纪念院落里成功举办纪念活动，该活动主题为：纪念林昭精神、追忆历史、唱红歌、时空和谐座谈会。（以下简称：忆林 昭、时空和谐座谈会）林 昭精神就是追求正义真理、关心国家民族命运，说真话的精神。<br />
<br />
 事先在网络上发布了要举办这次纪念活动的消息，并且邀请了一些社会活动的活跃者来参加，甚至对在网上有时候争论得很急烈的各方也都进行了邀请，还邀请包括当地领导干部如有兴趣和时间也不仿来此座谈会一聚。在这之前，在农家院子里设立林 昭大型雕像的事也和当地干部进行了通报。并且在院子里杏花盛开的时候，有不少朋友都来观看在花影下的林昭仿铜雕象，把这些影像在网上公布后一些大学的老师、博士生也来参观与雕像合影留念。<br />
<br />
 <br />
<br />
在林 昭蒙难纪念日当天，各界得知这一消息后前来参加座谈的人们，在大家把自己所知道的关于林 昭生平事迹互相补充陈述后，也对一些问题和看法进行了交流和争论，从林 昭所产生的时代背景到林 昭个人的历史定位，在座各位都进行了各自和表达。一些观点指出我们今天纪念林 昭就是因为要使像林 昭女士这样的命运遭遇永远不要再发生，指出我们现在仍然没有对我们的历史，包括文革史、及之前的近现代史进行回顾和总结，而是采取徊避、淡化、遗忘的态度方法。与会者认为产生林 昭悲惨命运的历史根源并没有总结，因此制造这种悲剧的可能性也就并没有消失，在追思林 昭的同时很自然地回忆了被当下有意无意被淡忘的中国所走过的道路。在痛感我们民族所失去的很多次走向健康的民主宪政的历史机会时，也对个人对历史所应负的责任和作用进行了争论。<br />
<br />
与会者中也有人指出了过分强调追责会促使社会部分人的恐惧心态，从而增加开启政改的难度和成本，又指出了中国政治路线的选择在客观上不是由某个个人的意志所决定的，中国的知识精英就其相当大的一部分而言，也曾起到了推波助澜、甚至主导的作用，因此把问题归咎为某个人的历史罪责作为反思整个社会所走过的弯路的根本原因，既不符合历史事实，也不策略，因此提出要呼吁提倡向政改的开启者们承诺不予追究已有的财产和个人责任。就此问题，与会者也进行了相当热烈的争论。<br />
<br />
本次忆林 昭、时空和谐座谈会在追思历史时i及的话题很多，从撕毁与前清退位皇室所制定的关于保留皇室既得利益，特别是把皇室赶出故宫所产生的恶劣历史后果、到东欧巨变后背离“园桌协议”给我国的政改所带来的负面影响。与会者也对目前社会上存在的非此即彼的简单化、情绪化、非理性化表示了担忧，在自由发言中大家对网上所争论的关于千人下跪罢免市长事件表达了理解和肯定。认识到要在追求民主的过程中学会理性思考，在让“一小部分艺术家先民主起来”的同时警惕走拉山头、搞邦派的歪道。追忆历史、拒绝遗忘，时空和谐、宽以待人、严以律己是解开政改死结的必由之路。<br />
<br />
 有趣的是本次忆林 昭时空和谐座谈会发起人周永阳在座谈会现场还唱了不少的红歌，并指出了创作这些歌曲时代的人们所抱的真诚信仰，而林 昭也有同样的早期信仰经历过程。对此有与会者也部分地接受了周永阳的忆林 昭唱红歌行为和重庆薄熙来“唱红打黑”有着“同曲异功”之妙。<br />
<br />
&nbsp;&nbsp;忆林 昭、时空和谐座谈会最后部分，是集体瞻仰安放于院内的林 昭纪念雕像，与会者燃香祭奠的同时现场朗诵林 昭诗歌，2010年忆林 昭时空和谐座谈会在一首首深情的诗歌朗诵声中圆满结束。<br />
