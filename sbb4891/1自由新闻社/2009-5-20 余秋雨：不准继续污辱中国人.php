													<h2>余秋雨：不准继续污辱中国人</h2>
						
						
																											<div id="postmessage_97669" class="t_msgfont">　在512汶川大地震一周年纪念之前，我约同几位朋友做了一件事，那就是请上海的中小学生每人选一本自己最喜欢的书，通过网上点击赠送灾区的同学，“九久网上书城”免费帮助完成这项赠送活动。我为两方的中小学生都写了一段话，其中说到：中国下一代对灾害的最平静回答，是朗朗书声。<br />
<br />
　　五月十日下午，我把万余册图书，亲自送到了灾区。灾区几所中小学都举行了隆重的接受赠书仪式，场面让我非常感动。<br />
<br />
　　赠书之后，我又为上海市援建都江堰市的几千名工程技术人员作了一次报告，说明一年来抗震救灾壮举体现了中国人的大爱至善精神，说明中华民族在集体本性上具有非常优秀的潜质，不输于世界其它民族。因此，我对上海市的工程技术人员说：“四川灾区是读解中华文明的一个大课堂，你们有机会来援建，是一种荣幸。”我的报告是在一个简陋的工地板房里做的，里边挤了两千多人，大家都汗流浃背，但三小时下来，全场肃静，掌声不断，没有一个人离开。<br />
<br />
　　在灾区的这段时间，我还参观了一些校舍的重建、文物遗迹的修复，又重新祭拜了被保留下来的废墟现场。我还访问了著名的救灾英雄、普通的受灾百姓和辛劳的基层干部，知道了这一年来他们每天要面对多少无法想象的难题，也知道了他们前面还有多少沟壑需要跨越。总的说来，那里是一片令人感动的奋斗景象。<br />
<br />
　　但是，完全出于意外，我的这些言论，立即受到了香港《O果日蟆芬桓鍪鹈“桑普”的人的猛烈抨击。他认为，我说中国人的“大爱”、“至善”、“生命关怀”、“以人为本”，全是“迷魂汤”。他向香港读者宣布，中国四川的512地震，“涂炭生灵，哀鸿遍野”，“人祸才是主因”。他甚至认为“真相早已摆在眼前”，“地震早有先兆”，他认为地震的灾祸是“官场腐败的结果”，“早在预料之中”。<br />
<br />
　　香港的几位作家读了这篇文章后立即告诉我，写这篇文章的，一定是大陆文人，因为口气、文风只能是大陆的。对此我真正愤怒了，不是因为它针对我。目前在全世界，包括那些对中国有对立情绪的国家，也没有一个人认为，512地震主因是人祸，而不是天灾；也没有一个人认为，中国人民在这场大灾难中表现出来的大爱精神是不存在的，是灌“迷魂汤”。<br />
<br />
　　美国没有人这样说，法国没有人这样说，日本没有人这样说，连台湾也没有人这样说。这次地震一周年，世界各国媒体又都来了，也没有一个记者这样说。但是，有几个黄皮肤、黑头发的中国人却这样说了，而且在国际媒体上振振有词。<br />
<br />
　　世界历史上很多地方都发生过地震，造成的生态灾难各有记述。去年的512地震，是历史上发生在人口密集地区的最严重的一次，直到今天，居然没有出现任何瘟疫、饥荒、逃亡、难民，这是全世界有目共睹的奇迹。为什么一定要闭着眼睛否认这个发生在自己祖国的奇迹，却去玩弄什么“生灵涂炭、哀鸿遍野”的词汇去诓骗外人呢？你作为中国人的良心放到哪里去了？<br />
<br />
　　说这种话，表面上是为了“生灵”，实际上是糟践中国，糟践中国人。我可以断定，这些人一定没有捐款，没有默哀，没有到过灾区，家人亲族中也没有人参加过灾区援救队和志愿者队伍。因为他们一再宣称任何捐款和援助都会让当地政府贪污。这种谎话，其实也在阻止来自世界各国的捐款和援助。<br />
<br />
　　我并不是说灾前灾后当地的行政管理工作没有问题。这正像，日本人来侵略中国的时候，当时中国政府的管理也有很多问题。但作为一个中国人，我们总不能说，日本人侵略的“主因”是我们自己造成的。当时的中国政府问题再多，我们也要坚决抗日，保卫祖国。把外来的灾难全都变成了自我讨伐，这就成了汉奸。六十年过去，时代已与抗战时期大不相同，在这次抗震救灾中，全世界都承认，中国政府、中国军队、中国警察、中国医生的表现，堪称优秀。全世界都承认了，唯独几个中国人彻底否认，我真不知道该把你们叫成什么！<br />
<br />
　　这个署名“桑普”的人，又一次攻击我去年对请愿灾民的劝说。好像是他站在灾民一边。其实，我当时急急地站出来劝说，正是看到有人要利用那些灾民做政治文章。这些人，明明知道在大地震现场要起诉房屋建筑质量是一件难事，将旷日持久，明明知道大瘟疫随时有可能流行、堰塞湖随时有可能崩塌，明明知道这些灾民在精神上极待援助，却铁着心肠要这些可怜的灾民长时间地跪在那里，来帮他们完成政治图谋，这实在是太冷血、太阴险了。你们要反对中国政府，可以换一个时间、换一个地点啊，而且也应该由你们自己来发言啊，怎么能让这些刚刚经历丧子之痛的家长成为你们的棋子？你们去问问那些家长，有哪一个会同意你们的政见？<br />
<br />
　　我是一个无职无权的独立文化人，居无定所，不交权贵，连作家协会和文联也没有参与，只是凭着我的亲眼所见和内心良知说话，如果按照“桑普”等人的所谓西方民主观点，我应该也有言论自由吧？我应该也有歌颂中国人、劝慰灾民的权利吧？为什么灾民们听从了我的劝慰而不听从他们呢？那么，他们又有什么理由来阻止我发言呢？因此，他们不得不伪造了一个阻止我的理由：仍然说我“文革”中有问题，参加过一个叫“石一歌”的写作小组。“桑普”这么一说，我立即知道他是谁了。<br />
<br />
　　我曾经一再说明，我在文革中全家受到迫害，只在林彪事件后参加过周恩来总理亲自布置的复课教材编写工作，那是一件光明磊落的大好事。但“桑普”等人故意把水搅浑，把周恩来布置的任务也说成是“文革写作”，还拉出一个“石一歌”的笔名硬按到我头上。我为此曾经发出悬赏，宣布只要有一个人能出示我用“石一歌”的笔名写过一篇、一段、一行、一句他们指控的文字，我立即支付自己的全年薪水给他，并在全国传媒上公布。我的悬赏进行了整整六百天，而且还写进了我在大陆和台湾同时出版的《借我一生》中，但六百天里没有一个人能指出一句，这该真相大白了吧？但可笑的是，这次“桑普”在《O果日蟆飞戏⒈淼奈恼律纤担我不仅是“石一歌”，而且还杀了人！<br />
<br />
　　这在香港，当然是犯了诽谤罪，是严重的刑事案件。但是，在香港打官司，费用是一笔天文数字，我付不起。也正因为这个原因，“桑普”等人最近几年都到香港去造谣诽谤大陆的人和事了。他们为什么做这样的事？当然只是为了钱。钱来自何方？他们自己知道。<br />
<br />
　　我最后要说的话是，诽谤我，你们已闹了十几年，我不想理会了；批判中国政府，这也是你们的权利；但是，要糟践全中国人民的抗震救灾，糟践中国人内心的大爱，糟践中国人在全世界面前的集体形象，我不可容忍。<br />
<br />
　　我年岁已经不轻，早已无所畏惧。今后要做的唯一事情，就是要从文化上回击那些向中华民族泼污的一个个利益集团，呼唤华夏子孙本应具备的尊严。这些年我在联合国世界文明大会上的演讲，以及在美国国会图书馆、哈佛大学、耶鲁大学的巡回讲学，都围绕着这个主题。512地震使我对这个主题产生了新的认识。在此，我想重复一下我在512之后半个月与两个美国朋友的对话――<br />
<br />
