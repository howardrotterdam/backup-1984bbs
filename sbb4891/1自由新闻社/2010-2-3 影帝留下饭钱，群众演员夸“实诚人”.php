													<h2>影帝留下饭钱，群众演员夸“实诚人”</h2>
						
						
																											<div id="postmessage_464272" class="t_msgfont"><a href="http://politics.people.com.cn/GB/1024/10915229.html" target="_blank">http://politics.people.com.cn/GB/1024/10915229.html</a><br />
<br />
阳光明媚，燕赵大地天气逐渐转暖。<br />
<br />
　　１月３０日，中共中央政治局常委、国务院总理温家宝来到河北省承德市滦平县巴克什营镇偏桥村看望村民，并在村民苏洪喜家中召开座谈会，就《政府工作报告》起草和改进政府工作与农民们座谈，听取意见和建议。<br />
<br />
　　“老苏，你的信我收到了，我来看看大家。”１０时许，温家宝在偏桥村村口握住６８岁的村民苏洪喜的手高兴地说。<br />
<br />
　　原来，５年前温家宝第二次来这个小山村时，曾和苏洪喜约定过５年后再来。不久前，一直惦记着温总理的苏洪喜以“农民老朋友”的身份致信温家宝，告诉总理这几年村子里的变化：修了水泥路，建起了饲料加工厂和种鸡场，自己家里也种了大棚，一年能收入２万多元。在信中，他代表全村人邀请总理再来做客。<br />
<br />
　　<br />
　　走出大棚，温总理和苏老汉手拉着手向村里走去。听说总理来了，村民们纷纷走出家门欢迎。温家宝就像回到家里一样，微笑着和乡亲们打招呼。<br />
<br />
　　随后，温总理走进苏老汉家。２０多平方米大的客厅里，摆放着一张普通的圆桌和二三十个三脚圆凳。温家宝亲切地招呼村民们坐下，客厅里坐满了人。温家宝开门见山地说：“今天到偏桥村，一是看望乡亲们。离我上次来村里又过去５年了，我很想念你们。二是３月份要开全国两会，我要在全国人代会上作《政府工作报告》，农业、农村、农民还是放在政府工作的重要位置。如何做好‘三农’工作，我想听听你们的意见。”<br />
<br />
　　在询问村子里的发展变化情况后，温家宝说：“我先把政府去年做的几件事，给乡亲们汇报一下。第一，我国农业连续６年丰收，为今后的发展奠定了基础。这在过去几十年的历史上没有过。第二，农民收入又提高了。去年全国农民收入增长８．４％，增幅是比较高的。第三，去年全国支农资金又增加了。中央财政预算用于安排‘三农’的支出达到７２５３亿元。第四，实行‘家电下乡’、‘汽车摩托车下乡’政策。第五，农田水利等基础设施得到改善……”<br />
<br />
　　“对这些事，乡亲们满意不满意？”温家宝问道。<br />
<br />
　　“满意。”村民们高兴地回答。<br />
<br />
　　“我再给大家说说今年要办的几件事。”<br />
<br />
　　从增加涉农补贴到提高粮食收购价，从进一步开展农村养老保险试点到提高新型农村合作医疗保障水平，温总理给村民们带来了一连串好消息，“财政收入新增部分要大部分用于农村、农业和农民，这是一条重要原则。”<br />
<br />
　<strong>　“这些年，国家净给农民搭钱了。”村民赵长发的话，引起一阵会意的笑声。</strong><br />
<br />
　　“对政府工作，大家有什么意见尽管提。”温家宝微笑着鼓励村民们。<br />
<br />
　　“总理，我提个意见。我们村二组的土地，三年前被征了２００多亩，每亩补偿１．８万元，这有些低。我们反映三年了，希望政府能多给我们一些补偿。”村民赵银山发言。<br />
<br />
　　这时，滦平县委书记毕建华解释道：“这块地是河滩地，政府征用后让北京的一家饲料企业在这里落户，带动养殖业发展。当时是按国家标准足额给的补偿。这家企业在县城中心规划区也征了地，每亩补偿要比这里多。征地类型不一样，价格也不一样，有些群众心理不平衡。”<br />
<br />
　　“征地补偿是个大问题。”温家宝认真地说：“要稳定农村基本经营制度，摆在第一位的是土地问题。工业用地、城市建设用地，第一要坚持农民自愿，第二要给农民合理补偿。土地是农业的主要生产要素，征地补偿起码要让农民感到合理。”<br />
<br />
　　“总理，我们家大棚里的黄瓜卖不上价，加上农资价格不断上涨，种黄瓜的收入还赶不上打小工的收入。”村民张保林的话，引起现场几位村民的共鸣，也引起温家宝的重视。<br />
<br />
　　“一斤黄瓜卖多少钱？盖一个棚子花多少钱？”温家宝认真地问着。地方负责人告诉总理，这里的大棚菜卖不出价钱，主要是没有形成规模，产销间没有通畅的衔接。他们正考虑把农业经纪人引到这里来。总理说：“这可是个大事。农民辛辛苦苦建起的大棚，本来是指望它增加收入。这件事，省里县里都要想想办法。不光要组织农民生产，还要组织农民销售。”<br />
<br />
　　“总理，现在粮食价格还是低，种粮收入上不去，影响农民的积极性。”村民白桂华接着又向总理反映道。<br />
<br />
　　“粮价偏低确实存在。现在粮价是放开的，靠市场调节。政府的作用是在粮价低时启动最低收购价来托市。这几年我们不断提高最低收购价，但价格提高应该是逐步的，需要统筹兼顾，从总体上考虑物价问题，考虑城市低收入群体生活问题。你们这里只种玉米，米面都得从外面买。粮价高了，也会把其他产品的价格带起来。”听了总理的解释，村民们纷纷点头。<br />
<br />
　　真诚的提问，坦率的回答。农家客厅里，总理与村民们以诚相见，畅所欲言：<br />
<br />
　　――“农村孩子现在都能上得起学了，但农村教师需要培训和提高。集中办学后，农村教师的住房问题也需要进一步解决。”小学校长王久吉反映。<br />
<br />
　　――“我们养殖户的困难主要是周转资金不足，抵押困难，贷不到款。”养猪专业户李连生站起来说。<br />
<br />
　　――“乡镇卫生院的设备现在比较齐全了，但是乡村医生想到外面学习很困难。能不能通过远程教育培训乡村医生？能不能帮助实现远程会诊？”镇卫生院医生常艳山接连提出了两个问题。<br />
<br />
　　面对村民们的问题和建议，温家宝一一作了回答。<br />
<br />
　　……<br />
<br />
　　“大家还有问题吗？”温总理还希望多听听村民们的意见。<br />
<br />
　　“现在潮河水资源越来越少。要加大水利设施的投入。”这时，镇农业助理娄亚臣建议。<br />
<br />
　　“水很重要。”温家宝点头说：“潮河水注入密云水库，这里是北京的水源地。要把滦平水源地治理纳入规划。”<br />
<br />
　　不知不觉，已是中午时分。两个多小时里，１２位村民先后踊跃发言，提出意见和建议。<br />
<br />
　　“从大家提的意见中，我感到政府要做的工作还很多。有些事情我们想到了，也写到文件上了，但落实还有一个过程，还需要大家共同努力。”温家宝说。<br />
<br />
　　“我来偏桥村三次了，感到这里生产搞得比较好，大家也很努力。”温家宝深情地对村民们说：“我给你们提三点建议：第一，把村容村貌搞好，让家家户户更干净些；第二，把土地规划好，利用好；第三，多种些树。这样，适当时候就可以发展旅游了。到那时，你们就不愁黄瓜卖不出价钱了。”<br />
<br />
