　新京报：是怎样的话让你感动了？<br />
<br />
　苏洪喜：总理说：“老苏，我跟你打听一个人。”总理打听的是2000年来视察的时候见的一个老人家，因为总理当时跟老人家聊救灾、自救聊得特别好。10年过去了，总理还记得他，我就觉得，我们总理真是实诚人，说的都是实诚话儿<br />
<br />
<br />
　总理说退休后还要来村里<br />
<br />
　新京报：座谈会结束之后你邀请总理留下来吃午饭了？<br />
<br />
　苏洪喜：12点左右座谈会就结束了，总理自个儿就说，今儿个我就在你们家吃饭吧。我当时就愣住了，觉得不可能吧，难以想象总理要跟我们家人一起吃饭。<br />
<br />
　新京报：为什么觉得不可能？<br />
<br />
　苏洪喜：我们吃的农家饭，不知道饭菜的卫生质量达不达标啊。于是我们家人开始愁到底做啥给总理吃。后来总理的秘书就点了菜，都是家常菜，噼里啪啦一下就做齐了。<br />
<br />
　新京报：吃饭的时候发生了什么有趣的事情吗？<br />
<br />
　苏洪喜：就是上炕的时候，我跟总理都推来推去，让对方先上。后来我就说，总理你来了就是客人，就得照我们农村的规矩来办，客人先上炕。后来总理就跟大家一样，把鞋一脱一撂上了炕，我和老伴就坐在他两边。<br />
<br />
　新京报：大家聊了些什么？<br />
<br />
　苏洪喜：我就代表村民们表达了一下对总理的谢意，总理就问我和老伴去过北京没，让我们养好身体，有机会一定要去，说北京的变化可大了。总理吃的时候也不住地夸奖我儿媳妇的饭菜做得香，一口接一口，吃得可带劲儿了。<br />
<br />
　新京报：后来合影是你主动要求的吗？<br />
<br />
　苏洪喜：哪好意思主动要求？虽然心里想着这个事儿，但觉得本来总理跟我们吃饭就很难得了。总理主动提出来合影，我们全家都高兴坏了。<br />
<br />
　新京报：总理走的时候，他说了什么没有？<br />
<br />
　苏洪喜：当时我们都去送总理，我儿子就大大咧咧地问：“总理你啥时候再来？”总理就说：“等着吧，就算等我退了休，自己也一定过来。”<br />
<br />
　<br />
　据新华社电</div>

							
							
							
							
