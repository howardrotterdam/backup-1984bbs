<br />
　　但凡有过上海体验的人都知道，上海人素有穿睡衣上街的习惯。弄堂里，菜场上；超市里，马路上；甚至在著名商业街南京路上也屡见不鲜。穿着花睡衣的女人，脚踩一双还算精致的皮鞋，跑到弄堂口买一包咸盐，或者顶着满头发卷出来倒垃圾&#8943;&#8943;这被看作是上海市井文化的典型画面。然而，当推土机推翻一条条弄堂，人们对旧时的生活方式只剩缅怀，残留的睡衣习惯骤然成为“文明”的大敌。<br />
<br />
　　2010年上海即将召开世博会，在这次代表现代文明的盛会里，小市民的“陋习”无法再被容忍。作为距离世博场地只有两三站路的齐八小区，跟浦东所辖的所有世博区域一样，面临现代文明的打量。<br />
<br />
　　上海市民们必须经受住这种国际化目光的打量。<br />
<br />
　　“这是国家的脸面问题。”沈国芳说。<br />
<br />
　　迎世博，睡衣睡裤不出门<br />
<br />
　　齐八小区的文明着装劝导队每周活动两次，每次一到两个小时。沈国芳说，来参加劝导队的“志愿者”，雷打不动的成员有10人，每次都佩戴红绸带，衣着整齐地立在小区门口，看到有穿睡衣的居民要走出小区，志愿者便会上前劝阻。<br />
<br />
　　“我们挑选的志愿者都是觉悟比较高、对社区比较热爱、对世博比较关心的居民。”沈国芳说。<br />
<br />
　　“在短短一个多小时里接受劝导队宣传的居民就有几百人次，活动收到了很好的社会效应。”齐八小区的网上日志中这样记载活动首日的“成果”。<br />
<br />
　　活动到现在已经进行了两个多月，“效果不错，穿睡衣睡裤的人明显减少了”。沈主任很满意。<br />
<br />
　　至于那些一见到劝导队就扭头往回走的居民，沈主任觉得，“避避开”也说明有进步，起码有文明的意识。<br />
<br />
　　几乎在同一时间，浦东新区世博场地附近的各街道、居委会也都没闲着，积极响应上海妇联和三林世博功能区域管理委员会的号召和要求，抓好文明着装问题。上钢社区、川新社区、北蔡镇等等，都在竞相开展类似的活动。<br />
<br />
　　苏美芝是齐八小区文明着装劝导队的一员，她在居委会主管妇女和计划生育工作。<br />
<br />
　　在从事劝导工作前，苏美芝说自己“也有穿睡衣(出门的习惯)”。但经过劝导队的培训，她“更加注意自己的着装”，还“优先”劝导家人不要穿睡衣睡裤出门。<br />
<br />
　　居委会的工作人员转变了观念，接着便是倡导更多的群众接受先进理念。但多年的习惯，撼动不易。为此，沈国芳他们也动了不少脑筋，想出许多劝导的招式。<br />
<br />
　　<strong>招式之一是收编放暑假的小孩子加入志愿者队伍。</strong><br />
<br />
　　“他们拉着阿姨叔叔的手说：‘阿姨(叔叔)，你不能穿睡衣睡裤到马路上去。’大人们听了也非常感动啊，觉得小朋友都在讲了，不大好意思了哦。”<br />
<br />
　　<strong>招式之二是强调世博的严肃意义，以达到震慑的效果。</strong><br />
<br />
　　“世博会时，外国人参观完园区出来以后拿着相机到小区乱窜，很有可能到我们小区来。”“我们是主人翁”，“小事放到台面上来就是大事了”，不能给上海丢脸。<br />
<br />
　　招式之三是利用熟人关系，注意说话方式。<br />
<br />
　　“你要是太正经跟人家交谈这个问题，人家有时候不能接受的。”沈国芳说。对此，他的方法是“打岔子”+“开玩笑”。<br />
<br />
　　比如，迎面走来一个穿睡衣的老熟人，沈国芳就会上前说：“哎，到什么地方去啊？”“买菜去啊？”“咦，怎么穿这个衣服出来了？” “以后不能穿了啊，这个衣服不好看，回去换掉。”兜了个圈子后回到劝阻穿睡衣的问题上来。<br />
<br />
　　苏美芝更是会把工作做到规定时间之外。对一些首次劝说无效的居民，她也会私下里反复跟人家说，大道理小道理地讲；当然，口气很柔和，脸上带着笑容。<br />
<br />
　　“国际大都市，最起码这一点，着装要做好。不是要你穿名牌啊，但要得体，里外要分清楚，这样更显示一个大都市居民的素质吧。”苏美芝说。<br />
