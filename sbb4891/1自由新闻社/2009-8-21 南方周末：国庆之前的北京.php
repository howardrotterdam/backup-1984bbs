													<h2>南方周末：国庆之前的北京</h2>
						
						
																											<div id="postmessage_210743" class="t_msgfont"><strong>真理部要求删除的文章</strong><br />
<br />
南方周末记者 李梁 实习生 王存福 祝莹莹 李耘耕 <br />
发自北京 2009-08-19 23:57:18<br />
<br />
&quot;■ 除了可以想到的武警、便衣、乞讨者、戴着红袖章的大爷大妈，外地人不易想到的小学生、大学生、公务员、出租车司机，也成为与六十周年大庆关系密切的人群。&quot;<br />
<br />
&quot;■“十一”日益临近，北京的发条越拧越紧。六十周年国庆，作为一个宏大的政治仪式，像一根线，牵着众多人、众多单位，以及北京生活的方方面面。 &quot;<br />
<br />
&quot;■ 尽管北京致力于成为中国的文化和经济中心，但在此刻，北京告诉你，政治中心才是它的底色。&quot;<br />
<br />
快满11岁的明明，从来没有起这么早。8月15日凌晨1点，他在闹钟的嗡鸣声中起床、洗漱，然后和妈妈一起出门，赶到集合地，和同学们汇集，等待大巴将他们送到京郊的良乡机场。<br />
<br />
明明是北京市海淀实验二小五年级学生，这一天，他要参加国庆游行方阵的大集训。良乡机场是国庆六十周年庆典群众游行队伍的集训地之一。<br />
<br />
如果坚持到10月1日，明明将成为10万名国庆游行群众中的一员。他将和小朋友们一起组成“星星火炬”方阵，手摇鲜花编织的花环，用稚嫩的嗓音喊起规定的口号，然后在国家领导人的目光中，蹦蹦跳跳地走过天安门。<br />
<br />
今年是国庆六十周年，秉承十年一大庆的传统，一场规模空前的阅兵式和群众游行即将上演。倒计时的日子里，这座城市的发条越拧越紧。<br />
<br />
群众游行“大集训”<br />
<br />
凌晨4点，明明和同学们到达良乡机场，等待他们的是上万人的盛大集结场面。他们每人领到了一份早餐一条长裤，而后耐心地等待组织集结。<br />
<br />
据报道，今年国庆将有10万人参加游行，共计46个方阵，大概是和平时期最为庞大的集体活动之一。游行队伍中，相当一部分是学生。备受瞩目的北京大学，参加国庆游行的大学生有3600名。<br />
<br />
参加游行的大学生们暑假要留京集训，学习群众游行的各项“规矩”。可一些学校通知较晚，部分参训学生已订好离京回家的车票。北京铁路局决定网开一面，在各站设立“学生退票专口”，不收取退票费。<br />
<br />
游行队伍还包括千名来自中直机关和北京市直机关的干部。一位北京市直单位的公务员告诉本报记者，单位领导们在各处室反复做工作，最终抽调了一些年轻人。进入名单的人，都经过严格政审，要求登记包括身份证号、私人住址在内的详细个人信息。<br />
<br />
8月15日的大集训是一场“万人预演”。或因人太多路难行，参加大集训的人们，在交通上享受了高级待遇。一位参加游行的学生在网上得意地写道，在去机场的路上，“无论是四环五环六环还是高速，入口全部被警车挡住，等我们车队通过后才放行。路上无论红灯绿灯，一律以我们通过为准！”<br />
<br />
清晨的风中，参加大集训的人们手持花束，随着队伍缓慢蠕动，等待入场。主席台方向，传来了毛泽东、邓小平和Jiang Zemin讲话的声音，以及录制而成的长时间的掌声。<br />
<br />
一位参加训练的某校大学生说，训练主要是两项内容，一是齐步走；二是手上动作。各个学校方阵都不一样。他们手持太阳花，北大拿的是风车和棒子，首体举着篮球，北京科技大学拿着太阳能板，民族大学的更有意思，“他们是跳着走过去的！”<br />
<br />
因为人太多，队伍组织的时间，往往要比训练的时间长。一位干部参加者清晨6时到达目的地，又等了两个小时后才轮到走场。而彩排最关键之处，不过是走过主席台的那几步，“不到五分钟就完事了”。<br />
<br />
训练的辛苦自不待言。因为大多数时候是站着，明明在偌大的机场上走了两趟就气喘吁吁。但他还是蛮高兴的――如果没有这次训练，他的假期肯定要在海淀的某个补习班度过了。<br />
<br />
大学生们也不赖，一些学生饭卡上突然多了300元钱，一些学生拿到了50元一天的补助，“几乎可以算是兼职了”。<br />
<br />
在某校论坛上，有同学写道：“火热的七月，挥洒的汗水，付出的艰辛，献出的精彩……”<br />
<br />
训练还将持续到国庆前夕。队伍最后将转换场地，进行一次更加逼真的彩排。像这样的预演还将进行数次。<br />
<br />
安保：超常规的手段和力度<br />
<br />
放假的学生们有时间每天排练，可出租车司机廖明俊师傅还得每天出车拉客。不过现在他们脑中同样绷紧了弦――遇见来路不明、行踪可疑之人，要迅速向警方报告。不久前，北京各出租车公司召集司机开大会，要求司机们国庆期间更加注重安全，尤其是在经过长安街的时候，对某些客人多留个心眼，“必要时尽量配合警方”。<br />
<br />
一切有如昨日重现――奥运会的安保措施，在人们的记忆里还未远去，今年又重新出山。<br />
<br />
北京市公安局启动了“惊雷行动”，“最大限度地将刑警便衣”，投入到“国庆庆典活动、重要敏感地区、繁华商业街区、各大旅游景点、公交车站”等地。市公安局刑侦总队政委王新元宣布，行动参照奥运标准，采用“超常规的手段和力度”。<br />
<br />
为保障国庆活动中的无线电设备使用安全，北京无线电管理局正在对全市的无线电对讲机、业余无线电台、校园广播、在用寻呼台全面清理整顿，重点是天安门周边、长安街沿线及阅兵村附近的无线电设备。<br />
<br />
高档酒店等涉外单位，也受到全面检查。据称，一天上午，一组公安局检查人员出现在王府井北口附近的皇冠假日酒店大堂。他们发现前台“外国人登记存在格式不符、英文名字过于潦草”等问题，要求工作人员迅速更正。<br />
<br />
天安门是安保的核心地带。8月17日上午，本报记者从天安门东站下车，然后绕道天安门西的地下通道，前往天安门广场，不到15分钟的路程，记者随身携带的电脑包被检查了三遍。<br />
<br />
广场上每隔十多米，就有武警和便衣来回踱步，目光四射。天安门广场东路和西路上每隔2分钟就会有一辆警车通过。<br />
<br />
像奥运期间一样，安保的另一支重要力量，是普通居民的“群防群治”。80万操着地道北京话的北京居民，戴着红袖章，将成为社区安保工作的主力军。<br />
<br />
8月18日，朝内小区里，五六位戴红袖章的大爷大妈们坐在树下一边聊天，一边注视着来来往往的行人。“最重要的是声势！”一位大爷说。<br />
<br />
北京城外，安检也比过去大为严格。在河北邯郸和北京之间来回奔走的徐先生对本报记者说，河北进京高速公路的收费站，也出现了一些安检设备。为配合北京安保，河北已提出筑牢“三道防线”。<br />
<br />
整修“夜以继日”<br />
<br />
有着“节日盛装”情结的北京，因为政治节日的临近，或者正在整修翻新，或者已经焕然一新。北京今年夏天出奇地炎热，但工程人员整修翻新的高效率同样令人惊奇。天安门城楼已经完成了修缮――城楼上的60根朱红色通天圆柱和36扇朱红菱花门扉，经过重新油饰后光亮一新，菱花窗格栅也重新贴金。<br />
<br />
截至本报记者发稿时止，人民英雄纪念碑的清洗装修尚未完成。从8月10日起，人民英雄纪念碑就被脚手架包了起来。<br />
<br />
天安门前，不仅有修，还有建。40余日前，在它的东南侧，出现了一间新邮局。邮局主要向游客销售5980元的《共和国珍邮》和4960元的《开国大典》和众多的纪念币等。8月17日上午，一位游客要求邮寄一本书，工作人员摆手：“邮寄东西请到正义路邮局，这里只寄平信。”<br />
<br />
北京的修缮工程中，规模最大的，当属长安街大修。天安门广场的一位工作人员告诉记者，大修主要是拓宽路面，同时加强路面抗碾压、防滑和减噪能力――10月1日，这条路将迎来排山倒海的士兵方阵，经受坦克、导弹等各式重型武器的辗压考验。<br />
<br />
事实上，长安街的整修今年3月就开始了。长安街沿线的上班族们，至少有一两个月不得不从坑坑洼洼的人行道上走过――那上面的砖块全部被挖开，落脚之处尘土飞扬。但是某一天，许多人惊奇地发现，人行道上铺起了整整齐齐的灰色新砖块；而宽阔的长安街路面，不知什么时候迅速铺上了一层柏油。<br />
<br />
忙于整修的不仅仅是那些贴有政治标签的建筑物，为了迎接国庆，北京相关部门检测、整修的目标，还延伸到电力，乃至120急救系统，都是。<br />
<br />
北京市电力公司也为供电的安全做了各种准备。电力专家们造访了北京饭店、北京电视台、火车站等多家重要单位，对他们的用电安全进行评估，确保国庆期间的用电安全。<br />
<br />
北京市120急救中心的急救车，最近全部配备了一个新玩意：滴眼壶。急救中心副主任万立冬向媒体解释说，国庆庆典活动当日，可能会有燃放未尽的烟火残渣从空中落下，伤害到观赏者的眼部，所以在急救车内配备滴眼壶，以方便治疗。<br />
<br />
消失的和离开的<br />
<br />
六十周年国庆，作为一个宏大的政治仪式，像一根线，牵着众多人，众多单位，以及北京生活的方方面面。<br />
<br />
北京各大单位，除完成上级任务外，都在组织各类庆祝联欢活动，忙得不亦乐乎。比如北京一所师范大学的一些行政官员和教师，最近就忙着参加国庆60年歌咏比赛训练。他们“四五点快下班时去练两三个小时”，反复练习《红星闪闪》、《心中想念毛主席》和《再过20年，我们来相会》等歌曲。<br />
<br />
北京各个街道都在忙活。西城区展览路街道办事处工委书记康莉，最近的会特别多，“多多少少都和国庆有关系”。<br />
<br />
也有闲下来的。北京的一家国际机构，将其8到10月初在北京的大型活动悉数取消。10月初，他们原本有一个关于某项社会问题的全球倡导活动,中国是重要国家之一,但为了不和国庆节的喜庆氛围冲突，最终决定另择吉日。<br />
<br />
8月18日，在建外SOHO工作的张先生去永安里小区吃饭，发现一大群人围在一起。他上前一看究竟：5个城管正在对一家小吃摊进行政策说教，要他拆除搭在路边的棚户。“国庆来了！”城管说。永安里小吃街上，很多小吃摊主已把厨具搬到了屋内。<br />
<br />
活跃在地铁车厢和长安街地下通道的乞讨卖艺者，也在不知不觉中消失了，同时消失的还有摆地摊卖各种小物件的小贩们。不过，国贸的上班族们从地下通道经过，仍然能偶尔听到一两个长发青年对着吉他长吁短叹。<br />
<br />
在北京南站附近的“上访村”，一位长期在某机关信访办公室胡同口卖东西的摊主告诉本报记者，近日前来上访排队的人数大大减少。这个信访办公室的绿色大门紧闭，几个上访者在门口席地而卧，一位来自河南省的张姓老人，正在抄录信访办门前的《来访人员须知》。<br />
<br />
更多的人想在国庆离开北京――今年国庆和中秋联合放假，将出现8天的“史上最长的黄金周”。一些北京人有感于北京拥挤的人流,计划出国度假。京城各大旅行社都推出国庆出境游,寄望通过国庆黄金周大赚一把。<br />
<br />
在国贸一家销售公司上班的赵云，正在和她的老公筹划去越南旅游。“建国60年大庆是大喜事。不过我就不掺和了，今年工作太累，准备找个地方休息休息。”<br />
<br />
明明和他的爸妈，可不会有赵云这样洒脱。负责训练组织的老师已经跟每个参加训练的小朋友们打了招呼：“尽量不要出京哦！”老师话说得很重：“这次训练是国家的大事，关系到你们的荣誉！”<br />
<br />
老师没明说，但聪明的明明已经意识到，和那些不参加集训的同学比，参加训练的同学，在来年“三好学生”、“优秀”称号评比上，肯定要占优势。<br />
<br />
明明很希望能够上电视，这是他对这次游行的最大期待。但在8月15日大合练这一天，明明站在长长的机场跑道上，看着前后黑压压的人群。他已经意识到，自己即使上了电视，也不过是人山人海中一个黑乎乎的脑袋而已。<br />
<br />
（文中明明为化名。本报记者赵凌、沈亮对此文亦有贡献，谨致谢意）<br />
