													<h2>世博参观攻略：朝鲜馆伊朗馆等无需排队</h2>
						
						
																											<div id="postmessage_594250" class="t_msgfont">这标题取得好<br />
<a href="http://expo2010.sina.com.cn/sight/p/20100502/03267931.shtml" target="_blank">http://expo2010.sina.com.cn/sight/p/20100502/03267931.shtml</a><br />
<br />
<img src="http://i2.sinaimg.cn/dy/expo2010/site/events/2010-05-02/U2123P1T1D20191833F21DT20100502032607.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
朝鲜馆内展示主体思想塔和城市风貌<br />
<br />
<img src="http://i0.sinaimg.cn/dy/expo2010/site/events/2010-05-02/U2123P1T1D20191833F23DT20100502032607.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
昨日，游客在朝鲜馆门前排队。记者海国、顾展旭摄<br />
<br />
　　本报讯 （记者蒋悦飞、何涛、吴红林）昨天，上海世博园区正式开园首日，几乎所有场馆均开馆迎客，世博园内人头攒动、热闹非凡。当天，不仅众多场馆扎堆举行开馆仪式，众多政要的接连造访也成了世博园区内一道风景：欧盟委员会主席巴罗佐来到了西班牙、比利时-欧盟馆，荷兰首相鲍肯内德到访了荷兰馆，沙特阿拉伯曼苏尔・本・穆泰・本・阿布扎勒・阿齐兹王子现身沙特馆……穿梭于世博场馆间，游客一不留神便可与政界要员们“零距离接触”。<br />
<br />
　　中国国民党荣誉主席连战、吴伯雄和亲民党主席宋楚瑜上午皆偕夫人来到中国馆。<br />
<br />
　　欧盟委员会主席最“抢手”<br />
<br />
　　开园第一天，众多国家馆集中开放，欧盟委员会主席巴罗佐成为欧盟各馆疯抢的剪彩嘉宾。昨天上午，他先来到了西班牙馆，接着又到欧盟比利时馆剪彩，行色匆匆，这个活动没有结束，另外一个欧洲国家馆的工作人员早已急不可耐地前来恭候。<br />
<br />
　　“这是欧盟第一次参与在欧盟以外国家举办的世博会。我们之所以这样做是因为我们重视欧盟与中国的战略合作伙伴关系。”在欧盟-比利时馆的开馆仪式上，巴罗佐再次强调。<br />
<br />
　　走进欧盟-比利时馆，你可以看到很多世界最先进的环保科技。站在建筑互动图书前，你只要挥挥手，投影仪上就会根据你的动作自动为你翻书，其中利用的是“光的感应技术”。你还可以看到蓝精灵、钻石饰品，买到19元6个的正宗比利时巧克力。<br />
<br />
　　荷兰首相吹响荷兰馆“球赛”<br />
<br />
　　昨日上午11时15分，在众多官员和记者的簇拥之下，一身西装革履的荷兰首相巴尔克嫩德走进了外观酷似游乐场的荷兰馆，成为荷兰馆的首名游客，这也代表着荷兰馆正式开放。当天，荷兰馆的开馆仪式被布置得极其简洁，当巴尔克嫩德给荷兰馆剪彩时，现场营造气氛的只有3名工作人员手中各拿的一个炮筒礼花，仪式不到一分钟就结束了。<br />
<br />
　　荷兰馆二层的一个小平台被布置成为一个小型足球场。在众人的注目下，巴尔克嫩德拿起哨子吹响“开场哨”，一名荷兰官员起脚将一只足球踢进了印着汉字“虎跃”的球门。这个精巧的开幕小节目充分体现出了荷兰人丰富的想象力和幽默的性格。<br />
<br />
　　沙特王子希望沙特馆永留上海<br />
<br />
　　昨天中午，上海世博会沙特阿拉伯馆举行了开馆仪式，沙特阿拉伯曼苏尔・本・穆泰・本・阿布扎勒・阿齐兹王子参加了剪彩仪式，并随后召开了简短的记者见面会。沙特王子说，这是他第一次来到中国，他对中国的印象非常好，“要是时间允许，我真想把园区里的所有展馆都逛个遍。不过，我知道这是不可能的。如果有机会，最想去中国馆看一看”。<br />
<br />
　　沙特馆是世博园区内仅次于中国馆的第二大国家馆，跟中国馆遥相呼应。沙特王子说，上海世博会是让世界人民了解沙特的一个很好途径，希望沙特馆可以永远保留在上海。<br />
<br />
　　沙特馆因其外形似一艘腾空而起的“月亮船”，且拥有全球最大的IMAX影院等亮点成为开园首日最受“热捧”的展馆之一。昨天，由于沙特王子亲临，沙特馆门前更是人山人海，为了一堵异域王子的风采，许多游客足足排队5个小时。<br />
<br />
　　台湾馆预约券半小时派光<br />
<br />
　　开园一小时后，就在中国馆旁边的台湾馆，4000张预约券也在不到半小时内派光。出于馆内的电梯间空间有限，钢化玻璃地面的承受力等因素，4000人是台湾馆一天的限定人数。<br />
<br />
　　在排队等候领取台湾馆预约券的时候，队伍中的记者发现，原本整齐的队形突然离散开来，身前身后的很多游客跟着跑上前，冲到台湾馆门口的围栏边，举起手中相机不住地拍照。<br />
<br />
　　在人群的缝隙中，记者看到一群黑西服人影，原来是中国国民党名誉主席连战偕夫人前来参观台湾馆。<br />
<br />
　　随后的参观表明，台湾馆是世博园中小而精场馆的代表之一。在台湾馆中，观众可以亲手点亮并放飞自己挑选的祝福天灯，在品尝了台湾特有的高山茶之后，还可以将印有台湾馆标识的精致茶杯“据为己有”，作为礼物。<br />
<br />
　　有些场馆无需排队也可看<br />
<br />
　　朝鲜馆工作人员<br />
<br />
　　胸佩金日成像章<br />
<br />
　　本报上海讯 （记者吴红林）上海世博园区正式开放首日，本报记者实地探访发现，一些精彩的场馆不需要排队即可入馆参观，比如，朝鲜馆、伊朗馆和阿富汗馆。此外，由于伊拉克最近才确认参加上海世博会，记者在世博园区并未看到伊拉克馆，在5月10日伊拉克馆日，游客有望欣赏到来自伊拉克的精彩演出。<br />
<br />
　　伊朗馆：<br />
<br />
　　游客参与编织巨型波斯地毯<br />
<br />
　　伊朗馆位于世博园区A片区。上海世博会伊朗代表团团长、伊朗商业部长卡赞法里曾经表示，伊朗馆最具特色的是一幅巨型波斯地毯，每个参观者都可以现场参与地毯编织，各地游客在6个月展期内共同完成这块地毯的编织，象征着世界人民团结协作。<br />
<br />
　　朝鲜馆：史上首次亮相世博会<br />
<br />
　　在世博会159年的历史上，上海世博会是朝鲜第一次参加的世博会。以“人民的乐园”为主题的朝鲜馆紧挨着伊朗馆，展馆以国旗、千里马铜像等图案装饰外墙，以主体思想塔、大同江、朝鲜式凉亭、假山、小石洞为主要展项，运用高科技手段展示首都平募裙爬嫌窒执的风貌和朝鲜秀美的风光、独特的文化以及在教育、科学、文化、体育等领域取得的巨大成就。朝鲜馆工作人员胸前都佩戴着金日成头像的像章。在朝鲜馆内，还放置着几台电视机，正在播放着反映朝鲜人民生活的录像，不少游客驻足观看。<br />
<br />
　　阿富汗馆：展出400多件宝藏<br />
<br />
　　阿富汗馆位于世博园区A片区，展馆的外形仿造历史名城赫拉特著名的蓝色清真寺。展示珍贵阿富汗宝藏的“拉希米收藏展”是该馆的最大看点，400多件罕见、独特的展品令人叹为观止。牧民帐篷成为游客们最乐意拍照留影的背景，帐篷内生活用品一应俱全，美丽的阿富汗女子站在帐篷最深处，一盏马灯散发出柔和的光芒。</div>

							
							
							
							
