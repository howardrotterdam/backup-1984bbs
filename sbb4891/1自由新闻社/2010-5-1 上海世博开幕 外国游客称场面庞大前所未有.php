上海世博昨日首天开园，再度失礼全球！中国馆五万张预约票被哄抢一空，有民众不满没票而大为鼓噪。园区内人山人海，一天下来，插队口角、乱抛垃圾、饮水机洗面等情况随处可见。不少游客失望而回，也有人大批：「永远进不了展馆，乱成这样不如别开！」<br />
世博园区昨早8时半举行开园仪式，全国政协主席贾庆林和国际展览局主席蓝（Lafon）宣布开园。为一睹园区真貌，不少民众一早到场等候。最近中国馆的五号门和六号门外过万人排队，长长的人龙一直延伸至地铁站口。大批公安在场维持秩序，由于人数实在太多，当局不得不提前在8时15分开园。<br />
一直备受观众「宠爱」的中国馆再次成为大混乱祸端。民众通过安检大关后，纷纷冲向闸内的公安拿中国馆预约门票，但五万张票开园不到10分钟就派完。「票！票！票！」六号门内数十名民众包围一名公安，几十只手指着怒骂，又箍颈又推撞，要求他拿出预约门票。「安静！等一下好吗！」公安喊破喉咙也没用，最后被群众步步逼退至跌倒在花槽内。<br />
