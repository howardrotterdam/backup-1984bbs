特写<br />
「人太多了，看到票都太激动」<br />
<br />
「我当时就一门心思想着要争取拿到票，一边是自己往前靠，一边是顺着人流的推动。」昨日世博园区「万客朝中」抢夺中国馆预约门票，上海市民蒲小姐也一样，为拿到票，早上未到7点就到园区外排队。当时现场已密密麻麻满是游人，大家七嘴八舌，同样在讨论如何去中国馆。<br />
为求一路顺利，蒲小姐入园前先向工作人员打听如何预约中国馆，开园后一出安检闸口就火速扫描哪里有中国馆门票。在距离她出安检口不到3米的地方围了好几圈人，她迅速冲前去，果然一名工作人员正在派发中国馆预约门票。<br />
<br />
冷气不足　场馆变蒸笼<br />
<br />
「人太多了，大家看到票都太激动了！」蒲小姐说，当时游客一拥而上，工作人员都招架不住。后面人潮的推动下，她刚好挤到工作人员面前，眼捷手快地拿到一张预约票。但并非人人都像她一样幸运，更多的人因拿不到而鼓噪。<br />
昨日上海气温高达摄氏30℃，烈日当空下，游客个个汗流浃背。园区各场馆排队一排就是两三个钟，场外又几乎都没有遮荫的地方，不少人开伞遮太阳，中国馆外有小朋友排了半个小时已直喊：「好热好热。」由于部份场馆采取局部自然通风，冷气不足下，场馆就像巨大蒸笼，游人在内像一只只F子，快被蒸熟。<br />
也有来自澳洲和法国的游客称，他们事前完全不清楚需要领取预约券才能进入中国馆参观，这反映事前宣传不足。<br />
