在沙地阿拉伯馆外，义工试图控制游客排队的秩序。路透社<br />
<br />
休憩点太少　报纸铺地午睡<br />
<br />
软件安排不周，硬件配套也未能满足需求。园内休憩点太少，烈日当空下，不少人见到阴凉处就蹲下或坐在地上休息，有人更报纸一铺就地午睡。<br />
原本用于回收胶樽、再经吸管集中收集的环保箱，一个也未能正常开启。民众不理垃圾桶是否分类，胶樽扔进纸品收集箱，食物残渣倒进胶樽箱。供民众使用的饮水机，再次成为人们洗水果、茶杯的平台，多部水机的去水位都留下茶叶。<br />
官方统计，昨日入园人数约20.4万人，全日园区垃圾达800吨。有民众一天下来看不到一两个场馆：「连进都进不了，更不用说拿免费品了，相信永远无法进入，只能看外观！」<br />
本报记者<br />
