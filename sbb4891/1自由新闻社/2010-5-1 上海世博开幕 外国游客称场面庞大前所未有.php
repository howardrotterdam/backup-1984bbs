<br />
# 2010-05-01 18:43&nbsp; &nbsp; 环球网<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 环球网赴上海世博记者李蓉、李洁思报道 2010年中国上海世博会于5月1日正式开门迎客，中外嘉宾陆续进园。对于世博开园第一天，老外留下了什么“初步印象”？评价如何？环球网记者于1日下午在世博园区随机采访了几位外国游客。在不少外国游客眼中，上海世博会“太大”了，有世博“骨灰级粉丝”的外国游客还说，过去几届的世博会从“场面”上来说，无法与上海世博相比。<br />
<br />
　　观点一：世博太大、人太多<br />
　　来自美国纽约的米希尔是跟团来世博参观的。虽然不是第一次来中国和上海，但谈起世博第一天的参观感受，米希尔坦言，自己还是对所有场馆充满了好奇，希望都能有机会把每一个场馆逛一遍。米希尔还很了解今天是世博“指定门票日”，对入院人数做了限制，他对环球网记者表示，从上海世博第一天入园情况看，排队的人还是比较多的，因此他对正常运行后世博园游客的管理有点儿“担忧”。当记者问他给世博第一天打几分时，米希尔给了满分。<br />
　　来自美国波士顿的劳拉和朋友都对整个上海世博会的环境竖起了大拇指，她们对记者说：“所有场馆都很漂亮。”不过，“人多，拥挤、长队”还是让这两位美国女孩给世博第一天打了7.5分。<br />
<br />
　　观点二：来世博找商机<br />
<br />
　　来自美国田纳西的拉-帕特尔在接受记者采访时说，他来世博会不仅仅是观光，还希望借此机会“发现一些商机”。<br />
　　“大，太大了，人太多了”， 帕特尔感叹道说，“我去过日本的世博会，那场面根本没法和中国上海的世博比。我很难想象中国能把世博办得这么宏大。”参观了非洲场馆、美洲 场馆后，帕特尔继续朝东走，他要在有限的时间里尽可能参观完所有的场馆。<br />
<br />
　　观点三：上海呈现了庞大而组织有序的世博<br />
<br />
　　德国小伙马克仅游览了世博的浦东园区就已经筋疲力尽了。由于一些热门的场馆客流都很多， 马克只能绕道去参观一些企业馆和国际组织馆，以避免数几个小时的排队时间。虽然进的馆不多，但马克对于上海世博会还是很期待的，他还表示，相信他第一天给世博的8分会在日后有所提高。<br />
<br />
　　作为世博会场馆的建设者之一，以色列馆设计师普罗斯佩・阿米尔则给上海世博首日打了9分。他表示，上海能在几年时间内呈现出这么一个组织有序的世博，他感到十分满意。不过他还提议，如果世博场馆开放时间更长，更灵活一些，比如24小时运营，这样有不同需求的参观者都能满足。</div>

							
							
							
							
