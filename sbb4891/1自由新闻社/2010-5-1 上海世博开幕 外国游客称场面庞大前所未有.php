假发眼镜被扯掉　老翁跌倒<br />
<br />
混乱中有人连假发、眼镜也被扯掉，有老翁被人推跌倒地，再被倒下的垃圾桶砸住，差点酿成人踩人。后来再有公安派预约门票，但眼见人群又开始失控，公安被吓至掉头就跑。<br />
「我5点多就来了，排在前面几个，竟然拿不到预约票！」60多岁的张婆婆进不了中国馆而失望。另一厢边，数千个旅行团则毋须排队预约，有团员指：「我们的票已经全部先预约好了，默认预定的。」有民众质疑预约票被人「内部贩卖」。由于不少人进不了中国馆也到馆外拍照，大批公安维持秩序，以防有人「冲馆」。<br />
