													<h2>国税总局局长称“中国税负痛苦全球第二”是恶搞</h2>
						
						
																											<div id="postmessage_851457" class="t_msgfont">国税总局局长称中国税负痛苦全球第二是恶搞<br />
2010年10月01日 10:05《中国改革<br />
比较分析宏观税负水平，不能脱离不同国家所处的发展阶段和政府职能范围，宏观税负水平没有放之四海皆适用的统一标准；中国的公共产品和服务需求处于快速上升阶段，宏观税负水平随着经济发展相应提升；由财政预算安排增加对居民的各种补助性支出，同样可以提高居民收入的比重，只是实现的途径不同；宏观税负水平的上升应随着经济发展循序渐进，必须把握好节奏和力度。而且，适度提高宏观税负水平要与深化税制改革和规范政府收入分配秩序等结合起来，使整体税负结构更加趋向合理<br />
<br />
肖捷 国家税务总局局长<br />
<br />
在现代社会，除了有意仿效鲁滨逊式的生存方式之外，要试图寻找一个脱离税收影响的空间是徒劳无益的。18世纪末，美国著名发明家、政治家本杰明・富兰克林(Benjamin Franklin)说过一句在西方国家广为流传的话：这个世界上只有死亡和征税是确定无疑的。<br />
<br />
现实生活中，税收负担问题受到社会各界普遍关注。微观税负的轻重，影响着纳税人可支配收入的规模，以及消费、投资和储蓄的能力；宏观税负的高低，则关系着社会资源和财富收入在国家与纳税人之间分配的格局，乃至经济运行和社会福利的状况。税收负担问题始终是治国理政的要务。在收入分配已成为中国现阶段矛盾焦点之一的情况下，对宏观税负水平的认识和把握，是进一步深化税制改革必须审慎对待的问题。<br />
<br />
税负痛苦指数的谬误<br />
<br />
关于目前中国宏观税负水平的认识和分析，除了基于学术和政策研究的讨论外，也有出于商业或其他方面的需要，由一些传媒或咨询机构发布的所谓调查结论。其中，国外某杂志发布的所谓2009年税负痛苦指数排行，一度吸引了国内媒体的关注。按其方法测算，中国税负痛苦指数为159，在其公布的65个国家和地区中排列第二，仅次于欧洲某国。对于诸如此类的评论，如果不加甄别，把戏说当作正史来解读，甚至作为学术研究的依据，产生的误导将贻害各方。<br />
<br />
――评价方法的常识性错误<br />
<br />
税负痛苦指数的评价方法，是确定六个税(费)种，包括公司和个人所得税、雇主和雇员交纳的社会保险金、销售税、财产税，并将其法定最高税率相加后进行比较，数值高低与所谓痛苦指数成正比(中国税负痛苦指数159，是由增值税最高税率17%、企业所得税最高税率25%、个人所得税最高税率45%、企业交纳的社会保险金最高费率49%、个人交纳的社会保险金最高费率23%相加得出)。虽然这六种税(费)涵盖了收益所得、货物劳务和财产类税收，在各国税制安排上具有普遍性，但其评价和比较方法极不科学，其谬误主要是将各税种最高边际税率简单相加，将税率等同于税负。<br />
<br />
税率是税制的基本要素之一，也是决定税收负担的重要因素，但不是唯一因素，税率与税负不能等同。确定税收负担的另一个决定因素是计税依据，即税基。税基与税率的乘积等于应纳税额。不同种类的税收，税基不尽相同，即便是相近的税种，税基也会存在较大差异。譬如，在货物劳务类税收中，销售税(营业税)的税基是销售额(营业额)，而增值税的税基是增值额，前者的税基要明显大于后者。尽管目前世界上实行销售税的国家的一般税率普遍低于实行增值税的国家的标准税率，但由于税基不同，不能简单地得出增值税税负高于销售税税负的结论。所以，用税率直接代表税负是一个常识性错误。<br />
<br />
――计算依据存在明显偏差<br />
<br />
将税率等同于税负的评价方法，也没有客观反映税率的真实状况。比如，中国现行个人所得税法规定了11项应税所得，对不同类型所得确定了不同税率。工资、薪金所得实行5%-45%累进税率，个体工商者生产经营所得等实行5%-35%累进税率，其他所得名义上统一实行20%的比例税率。将个人所得税的税率笼而统之地按工资、薪金所得的最高边际税率45%计算，在所谓痛苦指数(159)中占28.3%，显然有失公平。<br />
<br />
社会保险金虽然不是法定意义上的税收，但具有强制性，属于准税收收入，作为计算宏观税负水平的要素无可厚非。问题在于，将中国企业和雇员交纳的社会保险金的最高边际费率按72%计算，并占到所谓痛苦指数(159)的45.3%，明显与事实相悖。根据有关制度规定，中国现行养老、失业、医疗、工伤、生育等五种法定社会保险金的企业和个人缴费率，最高的是养老保险，企业和个人分别按工资总额交纳20%和8%，其他几个险种费率都不高。几项费率加在一起，最高值也达不到72%。<br />
<br />
将税率简单相加并以税率等同于税负的评价方法，既缺乏科学意义，也没有实际价值，中国对外开放特别是外商直接投资并未受此影响。统计数据显示，2006年-2009年，外商在中国非金融领域直接投资分别为：630.2亿美元、747.7亿美元、924.0亿美元、900.3亿美元，其中，2009年外商在华投资规模位居全球第二。<br />
<br />
与将税收负担冠之以“痛苦”之名形成对照的是，近年来，那些税制不透明、对有关所得没有或仅有名义上课税的国家和地区，受到了国际社会的严厉谴责。这些被视为“避税天堂”的国家和地区，由于为其他国家(地区)的企业和个人等避税乃至洗钱提供方便，破坏了税制的公平与公正，影响了国际金融体系的稳定，因而广遭诟病。2006年6月，经济合作与发展组织(经合组织，OECD)曾提出了一份包括40多个国家和地区在内的“避税天堂”名单。2009年4月，20国集团(G20)伦敦峰会期间，经合组织发布了一份对84个国家和地区实施国际公认税务标准的调查评价清单，在G20伦敦峰会发表的声明中，就此问题明确提出，对不合作的国家和地区，包括“避税天堂”采取行动。同年9月，在伦敦召开的G20财长和央行行长会议表示了更加强硬的态度，准备对“避税天堂”采取反制措施。<br />
<br />
如果说，有关税负痛苦指数的排行，是个别传媒制造的噱头或恶搞，那么，有关“避税天堂”问题则为国际社会所不容。在经济全球化的背景下，税收负担和税务透明度问题已经不再单纯是一个国家的内部事务，也要有国际判断标准并接受国际社会监督。<br />
宏观税负的比较分析<br />
<br />
目前，国际上衡量一个国家的宏观税负水平，一般是用一定时期(比如一年)政府集中的税收总量或税收和非税收入总量占同期国内生产总值(GDP)的比重来测算。经合组织(OECD)、国际货币基金组织(IMF)等国际组织，以及一些国家的政府机构，通常用此方法测算不同国家的宏观税负水平并公布这方面的数据。<br />
<br />
根据IMF《政府财政统计年鉴(2009)》所列的数据相对齐全的47个国家和地区2008年的资料(该年鉴在有关条目下列出了134个国家和地区，其中有59个国家和地区基本数据缺失，28个国家和地区数据未及时更新，年份不可比)，按税收收入计算的宏观税负水平如下：<br />
<br />
23个发达经济体的国家(包括比利时、塞浦路斯、芬兰、法国、德国、希腊、爱尔兰、意大利、卢森堡、荷兰、葡萄牙、西班牙、澳大利亚、加拿大、丹麦、冰岛、以色列、挪威、新加坡、瑞典、马耳他、英国、美国，以下简称发达国家)平均为27.7%，最高为47.1%，最低为14.6%；<br />
<br />
24个新兴和发展中经济体的国家和地区(包括毛里求斯、摩洛哥、南非、马尔代夫、泰国、保加利亚、克罗地亚、捷克、爱沙尼亚、拉脱维亚、立陶宛、波兰、罗马尼亚、斯洛伐克、亚美尼亚、白俄罗斯、摩尔多瓦、蒙古、俄罗斯、乌克兰、智利、洪都拉斯、秘鲁 、澳门，以下简称发展中国家和地区)平均为22.7%，最高为37.7%，最低为16%。<br />
<br />
按包含税收和政府非税收入等在内计算的宏观税负，发达国家平均为43.3%(其中，社会保险缴款占10.4%)，最高为58.7%，最低为21.7%；发展中国家和地区平均为35.6%(其中，社会保险缴款占6.9%)，最高为52%，最低为21%。<br />
<br />
相比之下，在这47个国家和地区中，发达国家按税收收入计算的宏观税负平均比发展中国家和地区高出5个百分点，这主要是由经济发展水平和政府履行职能范围决定的；按税收和政府非税收入计算的宏观税负，发达国家平均比发展中国家和地区高出7.7个百分点，这主要是因发达国家社会保险缴款等规模要超过发展中国家。<br />
<br />
在IMF《政府财政统计年鉴(2009)》中，中国的数据为2007年。按照中国现行各类预算管理制度规定，并以2009年数据测算，中国税收收入占GDP的比重为17.5%；加上政府性收费和基金等非税收入，宏观税负约为30%(其中，国有土地使用权出让收入占4.2%，社会保险基金收入占3.8%)。有的研究机构和学者，根据相关数据分别测算或估算中国目前宏观税负水平比30%要略高一些。笔者以为，其中可能存在数据转换口径的差别，或者没有完全剔除关联数据之间的重复计算因素。<br />
<br />
必须指出的是，用政府集中的各项税收和非税收入占同期实现的GDP比例来衡量宏观税负水平，虽然是目前一些国际组织和政府机构常用的比较方法，但仍然有其局限性。<br />
<br />
首先，国家间财政制度安排上的差异，会不同程度影响税负比较的公正性。由于各个国家的财政政策和相应的制度安排，分别体现了本国执政当局的治国策略和管理思想，即使是方向一致的政策主张，也可能会在操作层面做出不同的规定，并影响到宏观税负水平的变化。<br />
<br />
此类情况并非罕见。例如，基于某种政策意图，政府对企业或公民的财政支持，既可以实行税收减免方式，也可以采取资金补助形式。实行前一种方式，政府集中的税收减少；采取后一种形式，并不影响政府集中的税收，占GDP的比重也会相应高于前者。目前，世界上一些国家为减少税收漏洞，一般都控制实施减免税政策，采用由预算安排资金补助。也有一些国家或地区，为吸引资本流入，鼓励某些行业或区域发展，对企业实施不同形式的税收优惠。如果是采取税收减免方式，将直接减少收入；如果是实行税收先征后返，即将税收先缴入国库，再通过预算支出安排，将缴纳的部分或全部税收返还给纳税人，名义上统计入库的税收没有减少，但这部分收入并没有实际形成公共财政资源。<br />
<br />
其次，统计数据的非一致性，也会或多或少影响税负比较的合理性。尽管IMF对政府收入的构成及分类做了界定和说明，然而，囿于资料的匮乏，我们无法区分不同国家在税收和政府非税收入统计上的具体差别。<br />
<br />
根据IMF的分类，政府各类收入中，包括税收、社会保险缴款、捐赠收入、其它收入。税收虽然是各国收入中共有的项目，但是，存在着上述不同制度安排出现的迥异结果的影响，进行项目间的调整，也难以保证各国数据口径完全一致。社会保险缴款反映了不同国家养老等社会保险管理体制的差别，以税收筹集社会保险资金的国家，这项收入很少或不存在，而以非税形式筹集社会保险资金的国家，这项收入规模较大。譬如，欧盟一些国家虽然税收收入占GDP的比重不到30%，但社会保险缴款一般都超过10%。这也表明，尽管税收是构成宏观税负的主体，非税收入负担也同样需要关注。<br />
<br />
捐赠收入所占比重不大，尤其来自外国政府或国际组织的赠与收入，与本国创造的GDP并无关联。其它收入，包括各种财产性收入、出售商品和服务收入，以及罚金和罚没收入等，各个国家多寡不一，有的国家财产性收入超过了税收收入，也有一些国家这项收入缺失。另外，根据使用者付费原则，政府提供的某些服务(如办理证照等)，由受益者负担成本费用，并不增加非受益者负担。<br />
<br />
财政制度安排存在的差异和统计数据的非一致性，难免会给比较分析研究带来一些困难，使宏观税负问题的讨论出现不同认识，甚至陷入罗生门。由此产生的纠结实属正常，无需渲染。或许只有在国际间普遍建立政府会计制度后，对政府的各项收支和资产负债等情况进行全面记录和核算，才能得出更加合理的诠释。遗憾的是，受制于多方因素，目前能够建立政府会计制度的国家微乎其微。<br />
<br />
虽然用政府集中的税收和非税收入占GDP的比重测算宏观税负水平有一定局限性，但仍具有参考意义，可以使我们对宏观税负问题的认识更加全面和深入。宏观税负既有总体水平问题，也有结构性差异，不能忽略具体项目和负担对象。宏观税负水平高，并不意味着政府可以统筹支配的财力资源一定就多，非税收入的许多项目具有特定用途，政府无法转移使用(比如，社会保险缴款只能用于社会保障支出，中国土地出让收入等也属于这类情况)。宏观税负水平低，也并不表示政府使用的财力资源一定就少，政府可能还有其它融资方式(比如，政府发行公债等)。比较分析宏观税负水平，不能脱离不同国家所处的发展阶段和政府职能范围，各国的具体国情不同，宏观税负水平没有放之四海皆适用的统一标准。<br />
<br />
中国宏观税负的趋势判断<br />
<br />
宏观税负的经济内涵，显示了一国政府为社会提供公共产品和服务所占用的财力资源在该国当年实现的GDP中所占的份额，它不仅体现了政府履行职能与所需财力资源的关系，也反映了政府集中的各项收入与经济发展的关系。<br />
<br />
――公共产品和服务需求处于快速上升阶段<br />
<br />
纵观世界现代化历程，随着经济不断发展和社会文明进步，社会成员期望享有的公共产品和服务的规模、质量与水平也愈来愈高。今后10年是中国全面建设小康社会的关键时期，如何由一部分人先富起来，逐步平稳过渡到全体人民共同富裕，既关系到兑现已向世人宣告的庄严承诺，又涉及国家的长治久安。<br />
<br />
目前，中国经济快速增长中日益凸显的收入分配问题，已经成为社会矛盾的一个焦点。初次分配体现了效率优先，兼顾公平显然不够。收入再分配面临着平衡分配差距，促进社会公平正义的艰巨任务。必须着力调整和完善收入分配政策，加快健全公共财政体系，积极推进公共服务均等化，合理调整预算支出结构，扩大公共产品和服务的有效供给，尤其是增加社会保障、教育文化、医疗卫生、保障性住房等与改善民生密切相关的支出。这不仅是服务型政府应尽的职责和公共财政的应有之义，也是缩小收入分配差距，化解社会矛盾的有效举措。<br />
<br />
进入新世纪[26.72 -0.22%]以来，经过各级政府的共同努力，在构建公共财政基本框架的理念下，财政支出结构不断调整，与改善民生有关的支出迅速增长。2009年与2000年相比，财政社会保障支出年均增长19.6%，预算内教育经费支出年均增长20.8%，预算安排的医疗卫生支出年均增长25.4%。尽管如此，仍难以满足社会民众，特别是中低收入群体对民生改善的迫切需要。<br />
<br />
社会保障体系亟待加快健全和完善，要逐步将社会保障制度覆盖各类群体，并积极创造条件统一基本社会保障制度，随着经济发展和收入的增加，相应提高保障水平。按照有关部门的数据，目前中国财政安排的社会保障支出和社会保险基金支出，约占GDP的5%以上，还适应不了社会保障事业的发展要求。<br />
<br />
前不久颁布的《国家中长期教育改革和发展规划纲要》，明确到2020年要基本实现教育现代化，包括实现更高水平的普及教育，形成惠及全民的公平教育，逐步实现基本公共教育服务均等化。目前中国财政性教育经费支出占GDP的比重约为3.5%，实现纲要提出的到2012年达到4%的目标，尚需继续努力。要进入人力资源强国的行列，2012年以后，财政性教育经费支出占GDP的比重仍应保持合理规模。<br />
<br />
正在实施的医药卫生体制改革，提出要加快推进基本医疗保障制度建设，初步建立国家基本药物制度，健全基层医疗卫生服务体系，促进基本公共卫生服务逐步均等化。虽然近几年各级财政明显加大了医疗卫生经费投入力度，2007年至2009年财政用于医疗卫生的经费支出年均增长39%，但要全面完成医疗卫生体制改革的各项任务，这方面的支出还有待继续增加。<br />
<br />
保障性住房建设属于政府公共政策的重要内容，随着城镇化进程的加快和低收入群体解决住房问题的迫切性愈来愈强，中国保障性住房的建设任务十分繁重。今年各级财政安排的保障性住房支出约830亿元，到2012年末要实现基本解决1540万户住房困难家庭的住房问题，必须保证支出稳定增长。如果要逐步达到发达国家保障性住房的水平，还应付出更大的努力。<br />
<br />
上述几个方面的公共需求，目前都不同程度地存在供给数量不足、质量有待改善、水平需要提高等问题。对于低收入群体而言，显然无力通过市场化商业运作方式，根本改变在社会保障、教育、医疗卫生、基本住房等方面的弱势状况，只能依靠政府的援助增进自身的福祉。这就需要合理调整收入分配格局，在增加中低收入者收入的同时，一方面努力扩大公共产品和服务供给，着力改善民生，另一方面通过税收参与初次分配和再分配，为政府履行公共服务职能提供财力资源保障。目前，政府集中的税收收入还无法满足迅速增长的公共支出需求，有必要随着经济发展逐步合理提高税收收入占GDP的比重。<br />
<br />
――宏观税负水平随着经济发展相应提升<br />
<br />
世界银行及美国学者曾进行的一项研究表明，一国的宏观税负水平与该国人均GDP大体呈正相关关系，即随着经济发展，宏观税负水平会相应上升，达到一定水平后将趋于基本稳定。进入新世纪以来，中国国民经济实现了持续快速发展。2009年与2000年相比，GDP年均增长10.3%，人均GDP年均增长9.6%；与此同时，宏观税负水平也相应提升，按税收收入测算的宏观税负由12.7%上升到17.5%(平均每年上升0.53个百分点)，按税收和政府非税收入测算的宏观税负由17.8%上升到约30%(平均每年约上升1.35个百分点)，其中，国有土地使用权出让收入上升了4.1个百分点，社会保险基金收入上升了1.6个百分点。<br />
<br />
在全面建设小康社会的关键时期，通过加快转变经济发展方式，调整和优化产业结构，中国经济将继续保持平稳较快发展，经济增长的质量也会不断提高，税收增长的基础更加稳固。同时，随着税制改革逐步深化，税制安排趋向合理，税收筹集收入和调节分配的功能将进一步增强。在综合考虑结构性减税等因素后，下一个十年，中国税收增长的弹性系数总体上仍可能继续保持大于1的态势，税收收入占GDP的比重在目前17.5%的水平上有继续上升的空间和条件。<br />
<br />
近一段时间，关于调整国民收入分配格局的讨论中，有一种观点认为，政府集中的税收收入规模不宜再扩大，应让利于民，增加居民收入比重。这种意见并不完全客观。税收在筹集收入的同时具有调节收入分配的功能，财政在预算资金的安排中可以发挥转移支付的作用。逐步提高居民收入在国民收入分配中的比重，既要靠初次分配，也需要再分配。通过合理扩大政府税收收入规模，适度提高税收收入占GDP的比重，由财政预算安排增加对居民的各种补助性支出，同样可以提高居民收入的比重，只是实现的途径不同。<br />
<br />
面对目前失衡的收入分配格局和凸显的社会矛盾，在初次分配强调效率优先的社会价值理念仍居主导的情况下，从实现社会公平正义的要求出发，不仅需要逐步提高劳动报酬在初次分配中的比重，也要高度重视再分配调节的积极效应，保持一定力度的再分配，有助于缩小最终分配差距。否则，收入分配状况很难得到合理改善，提高居民收入比重的意愿也会部分落空。<br />
<br />
从这个意义上讲，税负水平的合理提升与促进社会和谐稳定并行不悖。这也是许多国家和地区通过调整分配政策校正市场分配扭曲的重要原因之一。国际经验表明，在税负合理增加的情况下，如果社会民众能够感受到政府明显扩大了公共产品和服务的供给，大多数人的福利状况得到了改善，纳税人对自己的生活状况普遍感到满意，通常会理解和支持政府的税收政策。<br />
<br />
上述分析旨在阐明，宏观税负是广受多项条件影响的矛盾体，需要辩证地认识和把握，不能简单地评论税负水平孰高孰低。今后一个时期，中国宏观税负不仅有上升的需要，也有提升的空间。如果政府收入不能满足履行公共服务职能所必需的支出，或者税收收入增长无法保证公共财政政策的连续性，政府只能要么通过发债等方式筹措资金，要么削减预算开支。<br />
<br />
实践证明，过度发债极易导致通货膨胀，受影响最大的是低收入民众，经济体也会受到侵害。目前，有些国家虽然宏观税负水平并不显高，但政府债务依存度却很高，有的甚至超过100%。IMF最近组织的一项调查显示，国债每增长10个百分点，平均每年会影响0.2个百分点的经济增长。一味削减预算开支也非长久之策，尤其是福利性支出具有刚性，直接关系社会成员切身利益，连续削减不利于稳固执政基础。<br />
<br />
需要指出的是，宏观税负水平的上升应伴随着经济发展循序渐进，必须把握好节奏和力度。如果税负水平上升过快、过高，不仅容易产生超额税收负担，挫伤纳税人的积极性，也会降低市场配置资源的效率，给整个经济运行带来负面影响。而且，适度提高宏观税负水平要与深化税制改革和规范政府收入分配秩序等结合起来。通过优化税制安排，减轻中低收入者的税收负担，合理增加高收入者的税收贡献，以此体现收入能力强的群体关心和帮助收入能力弱的群体。<br />
<br />
针对近几年政府非税收入占GDP的比重上升较快的局面，要统筹考虑税收负担与非税收入负担的关系问题，通过实行费改税等措施，进一步理顺政府收入分配渠道，规范非税收入秩序，在宏观税负水平随着经济发展稳步合理上升的同时，使整体税负结构更加趋向合理</div>

							
							
							
							
