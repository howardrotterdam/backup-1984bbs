																											<div id="postmessage_117125" class="t_msgfont">“我没有焦虑感，也没有拯救感” <a href="http://www.sina.com.cn" target="_blank">http://www.sina.com.cn</a>&nbsp;&nbsp;2009年06月05日 23:01&nbsp;&nbsp;经济观察报<br />
　　刘溜 <br />
<br />
　　经济观察报：据说王小波是作家中最早用电脑打稿子的，他是电脑高手。 <br />
<br />
　　连岳：他应该最早意识到网络给人带来的好处，他当时对信息自由流动的乐观情绪就已经弥漫在他的杂文里面，事实证明他的看法是正确的。他过世是在1997年，当时网络还是非常稀罕的东西。他有点儿小孩子对新鲜事物的好奇和天真，而且不停地尝试新东西带来的各种各样的乐趣。 <br />
<br />
　　经济观察报：很多人都在骂网络，比如网络滋生出网络暴民，网络并不能带来民主等等。 <br />
<br />
　　连岳：网络只是一个工具，我认为始终要把网络回归到工具，如果你在网络上是暴民，可能在现实生活中也是暴民，只不过你通过网络这个工具把你的声音给放出来了。一个正常的公民社会，是建立在每个人具备充分资讯的情况下的，我觉得网络很好地提供了这种可能性，从这个角度上它是一个非常好的工具。 <br />
<br />
　　现在网络让我们尝试了一下言论自由大概是什么东西。言论自由其实包含了大量的人身攻击、粗话、谣言，有语言暴力的言论，人身攻击的言论，以及制造谣言的言论，这是言论自由社会必然呈现的多样性状态。所谓言论自由就是你一定有发表错误观点的自由。你会发现越是言论自由的地方，让你讨厌的信息越多，这才是言论自由。 <br />
<br />
　　经济观察报：你生活得很超脱，但你有一句话，“我们自己就是体制的一部分”是吧？ <br />
<br />
　　连岳：我曾经在检查院工作过三年，我有很多好朋友也在现在的体制之内比如司法体系里面，其实他们中间有很多理想主义者，他们在自己力所能及的范畴之内，会尽量争取让法律来说话。我觉得所谓的整个社会在进步，一定是体制内外都有力量在进步。逐渐地人们都能具备公民社会该有的一些常识，虽然他可能仍然只能做很小的事情，但是这些常识给了他，你觉得重要不重要？我觉得很重要。 <br />
<br />
　　经济观察报：这个进步的过程很可能非常缓慢。 <br />
<br />
　　连岳：没办法，我觉得可能找不到更快的办法。有时候你可能太急迫了，希望这个社会一觉醒来会改变，或者希望你写一篇文章全都觉醒。你把时间放长以后，虽然每天没什么改变，但对照十年前和十年后的社会，我觉得十年后的社会宽容了很多，多样性也增加很多，新的一代人身上那种旧东西也少了。 <br />
<br />
　　经济观察报：你不着急吗？ <br />
<br />
　　连岳：我不着急，我对自己对社会都没有焦虑感，也没有拯救感。有人真的觉得自己能做很多很多事情，甚至能拯救他人拯救这个时代，这种想法是比较可怕的。其实你自己还有很多事情要去做，世界上还有很多好的东西，比如至少你可以享受生活，可以到比较舒服的岛上住一住，这不必等体制进步。 <br />
<br />
　　如果再过几十年社会还是这样子，那死的时候只能认命了，那就是这一代人的命运；只能等下一代人，看他们拥有的这个社会怎么演化。你是希望公众的意识有一个变化，如果几十年过去了，公众并没有体现出那么强烈的意愿去做这个事情，那你也只能认同他们的选择。 <br />
<br />
　　经济观察报：如果再过几十年社会还是老样子，你会怎么办？ <br />
<br />
　　连岳：如果我有能力的话我会尽量地享受生活，这是我自己努力工作应得的，我不欠谁，我谁也不欠。同时我能做的，就是评论这个社会现象，关注这一批人。生活有生活的逻辑，批评社会有批评社会的逻辑。 <br />
<br />
　　我相信是有一批圣徒一样的人物，宁愿放弃自己所有尘世的快乐，去苦行，去拯救别人，但这不是一种普遍可行的状态。 <br />
<br />
　　经济观察报：不能反过来要求其他人。 <br />
<br />
　　连岳：对，绝大多数人正常的状态是首先想着自己，然后在自己成本很低的情况下顺便帮助别人。所以我觉得绝大多数人的进步要建立在绝大多数人正常的状态之上。你可以希望他做事情，但是做这个事情对他来说成本一定是很低很低的。所以往往很多事情的推行，是能让很多人愿意顺便做这个事情，这个事情才能做成。 <br />
<br />
　　经济观察报：你是怎么从《中国可以说不》那种情绪中走出来的？ <br />
<br />
　　连岳：应该说大家都喝狼奶长大，我觉醒得特别迟，在1997年左右那一波民族主义狂潮当中未能幸免。在这点上我非常感谢王小波，他的一系列论证，让你意识到民族主义情绪的东西，在逻辑上是不可自洽的，也是违背人性的，你就会迅速地发现它是一个谎言。 <br />
<br />
　　你对自如发言的要求，你对财产的喜好，你对公平的竞争秩序的要求，这些都是本能。只要一个人愿意舒服，愿意快乐，他一定会追求这些东西的。 <br />
<br />
　　经济观察报：说到对财产的喜好，我们社会里好像“耻言利”的声音还是很大。 <br />
<br />
　　连岳：我记得奥巴马在欧洲说过一通话，说他对美国有信心，理由是什么呢？一是美国人还是很羡慕富人，不仇富；第二，美国人还是很爱钱。所以他对美国很有信心。 <br />
<br />
　　我们这儿还有很多人把 “惟利是图、大家都只想着钱”当成负面因素，用来描述这个社会已经堕落了，但是他不知道这个反而是社会最重要的动力，是人争取自己利益的很重要的途径。比如作为商人，巴菲特在赚钱的时候他可能会赚尽每一分利润，但是他死的时候会把所有钱都捐出来，卡内基也是这样。 <br />
<br />
　　在我们面前有一个很不好的习惯，无论是哪个阶层，受益阶层也好受损阶层也好，都很喜欢侮辱最大多数人很自然的选择，他喜欢钱你觉得他庸俗，他追求自己的利益，你觉得他没有大局观。 <br />
<br />
　　《国富论》已经解决这个问题了，人的自私自利就是这个社会最好最有效的动力。但我们到现在还把人想着自己的利益当成非常坏的事情。 <br />
<br />
　　经济观察报：谁不爱钱呢，基本上大家都很庸俗。 <br />
<br />
　　连岳：我觉得，人不庸俗的话，这个社会就是一个糟糕的社会。合理的社会一定是绝大多数人都是庸俗的，而且理直气壮地庸俗。 <br />
<br />
　　我们的社会我觉得是庸俗得不够，大家都还没有站到自己的角度考虑问题，好像每个人跳出来都是为整个社会考虑。你多去考虑一下自己，多争取一点自己的利益，这样庸俗的人多一点的话这个社会就进步了，就不庸俗了。 <br />
<br />
　　经济观察报：现在还有一些人批评上世纪九十年代后下海经商的知识分子只管挣钱，抛弃了理想主义。 <br />
<br />
　　连岳：我倒觉得，现在大家该做的，一个是发声，一个是发财，你能批评就去多批评，多说真话，你如果有经商的天才就赶快发财，钱和意见是一样的，都是重要的。 <br />
<br />
　　如果中国出现一大批又能发声又能发财的知识分子，这是最好的。我觉得知识阶层应该很有钱，应该要有一批非常有钱的知识分子。知识分子没必要为自己有钱，或者为自己生活的舒适而感到什么愧疚，这没什么好愧疚的。欧美做电视媒体的知识分子，很多人年薪几千万美元。知识分子一定要有钱，赶快有钱，有钱后批评的声音也会雄壮很多。 <br />
<br />
　　经济观察报：你觉得自己是一个公共知识分子吗？ <br />
<br />
　　连岳：我可能被一些人贴上了这种标签，但是我不认为我是一个公共知识分子，因为我觉得知识分子都是私人的，不存在所谓的公共知识分子。只不过当你的利益和这个社会的利益是一致的时候，你为自己争利益就是为社会争利益，这是顺便的行动，而不是说，我这个人或者某一些人是专门捍卫公共利益的。 <br />
<br />
　　经济观察报：你觉得一个人最重要的品质是什么？ <br />
<br />
　　连岳：独立。真的要做到独立也有点难度，第一需要能力，第二需要勇气。但也不是特别难做到的事情，你在现实生活中会碰到很多这样独立的人，你自己会喜欢这类人，因为这类人有力量。 <br />
<br />
　　独立最后肯定是会体现为独立判断，这也是这些人可爱之处，他跟别人是不一样的。他不能被人激将，不能被人家要挟，有时候不能跟多数人的判断走。 <br />
<br />
　　经济观察报：你写过一篇曼德拉，很赞他。 <br />
<br />
　　连岳：当然，我觉得曼德拉、甘地这种人物可以说是人类遗产。但同时，我觉得最可怜的就是大家把曼德拉、甘地给神话了，只要有一个人做到大家认为不可能做的事情，就觉得他是神，这是最可怕的一点。他们所做的事情，其实是很简单的逻辑，但没有人去做这样简单的事，他们做成了，然后人们就说：他是神，这个我们做不了。知识阶层的坏习惯是侮辱民众，而民众的坏习惯是等待拯救，大家都指望有另外一个人来做事，不是所有人都愿意做最简单的事情。 <br />
<br />
　　经济观察报：你最早被人所知是你的情感专栏，实际上情感关系也是最重要的社会关系之一。我们谈谈婚姻吧，你曾说现今社会的婚姻关系还处于非常蒙昧的状态？ <br />
<br />
　　连岳：对，我大概主持了六七年的情感专栏，对情感的现状会比较了解一些。大家先是急着结婚，在婚姻里都很害怕真相，对出现的冲突、问题采取鸵鸟政策。但其实结婚前你应该尽量多谈恋爱，因为你怎么样去爱一个人是一个学习的过程，找到适合你的人也是一个过程。 <br />
<br />
　　很多人的婚姻像演戏一样做给别人看，让长辈、父母开心，身处其中的人很痛苦。这是很可惜的，因为我觉得爱一个人可能是最幸福的事情了。无论什么样体制下的人，爱这个东西都是不会停止的，你总是可以去爱一个人，很多人连这个最基本的权利都放弃。 <br />
<br />
　　经济观察报：你觉得一个好的感情状态是什么样子？ <br />
<br />
　　连岳：可能也就是彼此接受对方，接受的过程是充分了解的一个过程，接受不仅包括接受他的长处，尤其包括接受他的缺陷、情绪，方方面面的缺陷。感情上的东西是希望平等对话的，你在感情中才会有意思，那种默契关系才会非常持久，你到老了可能都不会厌倦。你跟一个人待了十年二十年，一个眼神就可以交流，这种乐趣是很难替代的。 <br />
<br />
