													<h2>新人举行国庆主题婚礼 宣誓爱国爱党</h2>
						
						
																											<div id="postmessage_257772" class="t_msgfont"><a href="http://www.sina.com.cn" target="_blank">http://www.sina.com.cn</a>&nbsp;&nbsp;2009年09月25日03:17&nbsp;&nbsp;扬子晚报 <br />
<br />
<img src="http://i1.sinaimg.cn/dy/s/2009-09-25/U3938P1T1D18721419F21DT20090925131449.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
国旗下进行爱的宣誓。<br />
<br />
<br />
　　昨天，南京一对新人演绎了一场国庆主题的婚礼大片。1979年出生的新郎在新中国六十华诞之际迎来了自己成家立业的喜庆日子，“建完大国，现在是建立我们小家的时刻了。”<br />
<br />
　　婚礼充满国庆元素<br />
<br />
　　伴随着新闻联播的前奏，司仪海涛字正腔圆地播报，“中央电视台、江苏电视台、南京电视台，我们在钟山宾馆向您直播今天的婚礼。”随后，在《歌唱祖国》的伴奏声中，八个壮小伙举起一段长约8米的红色丝绸踢正步向前走，立正、站定，8个小伙子将红绸高举过头，1949年出生的长者、1969年出生的中年夫妻和1989年出生的年轻情侣携手走过红绸映照的长廊。<br />
<br />
　　最后，1979年出生的新人缓步步入礼堂，举起右拳在国旗下庄严宣誓，“我宣誓，爱国爱党爱着你。”台下的亲友团听到新郎与众不同的誓言，纷纷挥舞手中的红旗加油呐喊，“说得好！”宣誓完毕，新郎手捧一面鲜艳的五星红旗，献给新娘，“你问我爱你有多深，红旗代表我的心。我就像红旗上的小星星一样永远对着正中的你。”<br />
<br />
　　受天安门升旗仪式感染<br />
<br />
　　谈及将国庆作为婚庆主题的理由，新郎单进军介绍说，自己平常不善言辞，希望婚礼能有时代特色，给新娘带来惊喜。“我1979年出生时，恰逢国庆三十周年。今年恰逢国庆六十周年，选择在今年三十而立、成家立业也别有一番意义。”<br />
<br />
　　新娘柳燕告诉记者：“我们前阵子一同去北京看升旗仪式和阅兵式排练，现场那种庄严和隆重的气氛严重感染了我们，当时突然有了灵感。今年是新中国成立六十周年纪念，又是我们小家正式成立之时，有大国才有小家，为什么不做一个国庆为主题的婚礼呢？”<br />
<br />
　　新娘的父亲是1949年出生，身为共和国同龄人的他告诉记者，六十年来我们的国家取得了举世瞩目的成就，而这六十年来，自己成家立业，养儿育女，今天儿子成家，自己算是完成了为祖国培养下一代的任务。<br />
<br />
　　来参加婚礼的亲朋好友纷纷表示：“这样的红色主题婚礼，很特别，能把新中国成立和结婚结合起来，让人印象深刻。”记者了解到，与往年相比，今年国庆婚礼市场上“国庆味”十足，一些大的婚庆公司在婚礼策划上增加了国庆主题的元素。比如，在婚礼主会场布置国旗和国徽，让新人在国旗和国徽下宣读爱的誓言，同时祝福祖国繁荣昌盛；有才艺的新人还可以在婚礼现场对唱爱国歌曲；新人乘坐的花车也将插上小红旗，与市民一起分享国庆和婚礼的喜庆气氛等。国庆元素的婚礼正受到追求创意的新人的喜爱。<br />
<br />
　　柳扬 王亚楠 文/摄</div>

							
							
							
							
