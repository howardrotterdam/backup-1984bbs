													<h2>新华网北京１０月１３日电 题：中国总理的亲笔致歉信感动亿万读者</h2>
						
						
																											<div id="postmessage_280199" class="t_msgfont">任沁沁　刘欢　王建华 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;１０月１２日晚开始，一封中国国务院总理温家宝的亲笔致歉信在互联网上广泛流传，感动了亿万读者。 <br />
<br />
&nbsp; &nbsp; 在这封用毛笔黑墨写就的颇具书法美感的信件中，６７岁的温家宝总理用朴实而真挚、又不失“专业”的文字，对关于自己的一篇报道中的引语部分特别作了更正，并致以歉意。 <br />
<br />
&nbsp; &nbsp; 被誉为“平民总理”的温家宝在这封亲笔信中说：“新华社总编室：贵社昨天播发我的《教育大计　教师为本》一文，其中岩石学的分类，应为沉积岩、岩浆岩（也可称为火成岩）、变质岩。特此更正，并向广大读者致以歉意。” <br />
<br />
&nbsp; &nbsp; １０月１１日，新华社全文刊发了温家宝９月４日在北京市第三十五中学的听课感受和在座谈会上的讲话。温家宝总理对所听５堂课的感受都作了详细讲述，文中提到“对学生的回答，老师应因势利导，问他看过多少种岩石，知道名字吗？老师就可以讲岩石的分类：沉积岩、岩浆岩、火山岩”。 <br />
<br />
&nbsp; &nbsp; 稿件播发后的第二天，温总理即写信给新华社，更正应为“沉积岩、岩浆岩（也可称为火成岩）、变质岩”。 <br />
<br />
&nbsp; &nbsp; “温总理的字写得真好，还专门致歉，真是位可爱的老人啊！”有网友如此风趣地评价道。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;中国国家行政学院教授王伟在接受新华社记者采访时说：“温总理的这个做法非常坦诚，体现了高度的责任感。人非圣贤，孰能无过。我对总理的尊敬非但没有减少，反而增加了。”王伟说，作为一位总理，发现错误就立即改正，恰恰反映了他追求真理的精神，值得所有人学习。如果各级领导干部都能采取这样的态度对待工作中的哪怕一个非常小的失误，对于科学发展观的落实来说也是大有裨益的。 <br />
<br />
&nbsp; &nbsp; 温家宝总理在今年９月４日的那次座谈会上，曾对广大教师提出要求：要崇尚科学精神，严谨笃学。而温总理正是把这种要求严加于自己的身上。 <br />
<br />
&nbsp; &nbsp; 北京大学教授、著名文化学者张颐武说，一国总理亲自给媒体写信并以此向所有读者致歉，在第一时间纠正一个看起来很细小的错误，这避免了任何可能的误解。他说，这一方面体现了公众人物更加注重和媒体的互动，这对整个社会的进步来说非常有利；另一方面则体现了社会、政府部门日渐成熟、反应快速的公关意识。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;“公开透明地面对问题，反而使问题不再扩大。总理此举，不论是普通百姓，还是各级党政机关，都应当从中有所启示。”张颐武说。 <br />
<br />
&nbsp; &nbsp; 同样在１０月１２日，一张温总理为美国前国务卿基辛格博士亲手撑伞的图片也让亿万网友感动。在这张“撑伞图”中，温总理在雨中为这位“中国人民的老朋友”、８６岁高龄的基辛格打伞送行。 <br />
<br />
&nbsp; &nbsp; 网友“峰行天下”说：“总理是个好榜样，值得全国人民学习……这是中华民族的传统美德。” <br />
<br />
&nbsp; &nbsp; 网友蓝天说：“一方面是总理对中华礼仪之邦的优良传统的体现――尊敬长者；一方面是中国自信的表现，如果中国还是一个很贫弱的国家，在与外国政要打交道时，我想我们考虑得更多的是如何维护‘端着架子的自尊’。” <br />
<br />
&nbsp; &nbsp; 香港《文汇报》北京新闻中心执行总编辑彭凯雷说：“一封７０来字的亲笔信，一张感人至深的‘撑伞图’，展现了温总理的大国总理、谦谦君子风范。” <br />
<br />
&nbsp; &nbsp; 彭凯雷认为，这两件事恰好体现温总理尊重“二老”：尊重“老百姓”，亲笔写信为自己话语中的疏漏进行更正并谦逊致歉，是尊重读者和普通民众，体现严谨作风；尊重“老同志”和“老朋友”，如“撑伞图”和此前的数次探望季羡林等。 <br />
<br />
&nbsp; &nbsp; 对于中国领导人的朴实亲民，国人并不陌生。 <br />
<br />
２００８年９月４日，人民网“特别制作”的“什锦八宝ＦＡＮＳ圈”页面正式上线。作为中共中央机关报《人民日报》旗下的国家级网站，人民网此举被网友认为是官方正式认可“什锦八宝饭”――中共中央总书记、国家主席胡锦涛和国务院总理温家宝的粉丝团。短短几天内，注册成为“什锦八宝饭”的网民就约十万之众。 <br />
<br />
&nbsp; &nbsp; 胡锦涛和温家宝还分别于２００８年６月２０日和２００９年２月２８日，首次与网民进行在线交流。 <br />
<br />
&nbsp; &nbsp; “这是中国政治文明焕发出的别样风景。”王伟表示。 <br />
<br />
&nbsp; &nbsp; 温家宝总理在雨中考察工作时总是坚持自己打伞的细节，也一直被传为美谈。而总理先后八次赶赴四川地震灾区看望慰问灾民，为受伤群众让路、亲手为灾民炒菜等一幕幕感人至深的场景，更给公众留下无法磨灭的记忆。 <br />
<br />
&nbsp; &nbsp; 张颐武表示，一直以来，西方世界看待中国领导人，总是给涂上“威权主义”的色彩，温总理的一言一行，正是对这种“有色视线”的有力反驳。 <br />
<br />
&nbsp; &nbsp; “中央领导集体已经给了外界亲民的印象，关于温总理的这件事，我相信外媒的反应肯定又是正面和积极的，有利于进一步破除它们有关中国‘威权统治’的固有印象。”王伟说。<br />
