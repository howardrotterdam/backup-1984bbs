<br />
京华时报3月2日报道 中共中央政治局委员、中央书记处书记、中组部部长李源潮在最近一期《求是》杂志发表文章指出，要深入整治用人上的不正之风，抓住干部群众反映强烈的突出问题开展专项整治，不仅要继续整治拉票行为，还要对跑官要官、买官卖官问题进行专项治理。<br />
<br />
