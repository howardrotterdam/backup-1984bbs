<br />
一是完善制度体系。制定出台《党政领导干部选拔任用工作责任追究办法》等法规性文件，完善“一报告两评议”制度，加强干部选拔任用工作离任检查。<br />
<br />
二是加大查处力度。严格执行违规用人立项督察制度，坚持有案必查，查处一起通报一起。<br />
<br />
三是强化专项治理。抓住干部群众反映强烈的突出问题开展专项整治，不仅要继续整治拉票行为，还要对跑官要官、买官卖官问题进行专项治理。<br />
<br />
四是加大监督力度。坚持和完善干部任用前征求纪检监察机关意见制度，建立健全干部选拔任用工作全程纪实制度，加大对干部选拔任用工作的监督检查和巡视力度，畅通电话举报、信访举报和网络举报等监督渠道。建立组织工作满意度民意调查结果反馈和向社会公开制度，引导大家把压力转化为动力，使之成为提高选人用人公信度的有力杠杆。<br />
