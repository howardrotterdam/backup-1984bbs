																											<div id="postmessage_602494" class="t_msgfont">李源潮:让卖官者身败名裂 买官者赔夫人又折兵&nbsp;&nbsp;<br />
黄河新闻网&nbsp; &nbsp;2010-05-07 08:20:44 <br />
 <br />
&nbsp; &nbsp; 贯彻实施四项干部监督制度视频会议举行<br />
<br />
　　李源潮：全力提高选人用人公信度<br />
<br />
　　让卖官者身败名裂，让买官者“赔了夫人又折兵”<br />
<br />
　　中央纪委、中央组织部6日召开视频会议，对贯彻实施《党政领导干部选拔任用工作责任追究办法(试行)》等四项干部监督制度作出部署。中共中央政治局委员、中央书记处书记、中组部部长李源潮指出，要不折不扣执行四项干部监督制度，继续以最坚决的态度同用人上不正之风进行战斗，全力提高选人用人公信度。&nbsp;&nbsp;<br />
<br />
　　李源潮指出，党的十七大以来，整治用人上不正之风取得了新成绩新进展，但目前所取得的选人用人公信度相对提高是个易碎品，稍有松懈就会出现反弹。要清醒认识用人上不正之风的顽固性和危害性，增强整治用人上不正之风的强烈责任感和一贯危机感。<br />
<br />
　　李源潮指出，《责任追究办法》等四项监督制度，为深入整治用人上不正之风提供了有力的制度化武器。制度的效用取决于制度的执行。要严格执行这四项监督制度，领导干部要带头遵守。要加强对制度落实情况的督促检查，真正做到有章必依、执行必严、违规必究。要将有关执行情况在党内和向社会公开，让广大干部群众了解监督、参与监督、支持监督。<br />
<br />
　　李源潮强调，从严治党，关键是从严治吏。要继续以最坚决的态度整治用人上不正之风，加大对违规违纪用人行为的查处力度，重点开展买官卖官问题专项整治，让卖官者身败名裂，让买官者“赔了夫人又折兵”。要毫不动摇地推进干部人事制度改革，认真落实《2010-2020年深化干部人事制度改革规划纲要》，努力从制度上消除用人上不正之风滋生的土壤和条件，用民主、公开、竞争、择优的制度保证选人用人风清气正。<br />
<br />
　　中组部常务副部长沈跃跃主持会议，中央纪委副书记黄树贤、中组部副部长张纪南在会上讲话。重庆、河北、山东、河南和最高人民法院等单位在会上发言。省、市、县纪委书记、组织部长，市、县委书记，中央和国家机关、人民团体、部分国有重要骨干企业、中管金融机构和中管高校纪检、组织人事部门负责同志等参加会议。(记者谭浩) <br />
