中国青年报5月7日报道 中央纪委、中央组织部6日召开视频会议，对贯彻实施《党政领导干部选拔任用工作责任追究办法(试行)》等四项干部监督制度作出部署。中共中央政治局委员、中央书记处书记、中组部部长李源潮指出，要不折不扣执行四项干部监督制度，继续以最坚决的态度同用人上不正之风进行战斗，全力提高选人用人公信度。<br />
