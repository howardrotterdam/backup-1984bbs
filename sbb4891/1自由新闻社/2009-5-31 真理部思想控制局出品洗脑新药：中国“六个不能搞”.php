<br />
新华网北京５月３０日电&nbsp;&nbsp;为扎实推动用中国特色社会主义理论体系武装全党、教育人民，深入学习实践科学发展观，帮助广大干部群众深入把握社会主义核心价值体系，中央宣传部理论局组织编写了《六个“为什么”――对几个重大问题的回答》一书，近日已由学习出版社出版，在全国发行。 <br />
<br />
　&nbsp; &nbsp;《六个“为什么”――对几个重大问题的回答》紧密联系国际国内形势的深刻变化，紧密联系改革开放和现代化建设的实际，紧密联系干部群众的思想实际，深入回答了为什么必须坚持马克思主义在意识形态领域的指导地位，而不能搞指导思想的多元化；为什么只有社会主义才能救中国，只有中国特色社会主义才能发展中国，而不能搞民主社会主义和资本主义；为什么必须坚持人民代表大会制度，而不能搞“三权分立”；为什么必须坚持中国共产党领导的多党合作和政治协商制度，而不能搞西方的多党制；为什么必须坚持公有制为主体、多种所有制经济共同发展的基本经济制度，而不能搞私有化和单一公有制；为什么必须坚持改革开放不动摇，而不能走回头路等一系列重大理论和实践问题，对于在全社会形成统一的指导思想、共同的理想信念、强大的精神力量和基本的道德规范，巩固全党全国各族人民团结奋斗的共同思想基础具有重要意义。 <br />
<br />
