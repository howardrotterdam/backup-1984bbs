<br />
<strong>精彩节选</strong><br />
<br />
　　坚持以马克思主义为指导，就要回答为什么在意识形态领域指导思想只能是一元的，而不能搞多元化。实际上，古今中外，任何国家和社会，不管社会形态多么不同，不管社会思想多么复杂，但在意识形态领域，占支配地位的思想总是统治阶级的思想，或者说指导思想都是一元的。<br />
<br />
　　有人担心，强调指导思想的一元化，会不会影响哲学社会科学的繁荣发展，会不会影响学术和艺术上的自由争鸣，会不会影响我们学习借鉴资本主义国家有益文化成果呢？我们说，这种担心是多余的。<br />
<br />
