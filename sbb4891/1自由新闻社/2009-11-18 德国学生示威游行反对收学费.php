													<h2>德国学生示威游行反对收学费</h2>
						
						
																											<div id="postmessage_331553" class="t_msgfont">德国学生示威游行又一波――弗莱堡站<br />
<br />
全德国学生们又一次大游行了。弗莱堡也不例外。今天早上市中心的公共交通暂停。一大票人在经济法律系教学楼外的草坪上集会，激动的学子在临时搭的台上演讲、喊话，大家都野得很，标语飘飘，不少人带着亮纸皇冠，警察哥哥姐姐们就在边上瞧着。进了大厅，全然没了往日的整洁，挂着些黄气球，拉着些涂鸦的口号，不少搁着食物的超市推车。学校最大的教室　AudiMax 更从昨晚就被学生们占领了，很多人在那儿过夜来着，据说大家要在那儿呆上一周。。还有娃儿大白天在地上铺点儿纸板就和衣卧下了，看这守夜困的。。。学生们中午还在市中心游了一圈，德国人的秩序井然当然保持着，只是要穿过这滚滚的人流到马路对面还真要小小费些功夫。<br />
 <br />
这次大游行除了反对收学费之外，主要是抗议德国榱送国际接轨而开始引入的“学士”、“硕士”学制。德国学制以漫长闻名，榱烁母铮引入的学制虽然时间上压缩了，但学生的课业压力很大，且选课大概不够过去自由。非常多的德国学生主要都是靠学生贷款和平时打工作为经济来源，学费和新学制，便成了“两座大山”。游行的传单上有这样的口号：Selbstbestimmtes Lernen. Selbstbestimmtes Leben. Freie Bildung Für Alle.　自主学习。自主生活。人人教育免费。<br />
 <br />
傍晚时分，看到几个大妞张罗着晚餐，在大声的音乐和大堆的食物当中，切切煮煮，教学楼周围看到的都是年轻兴奋的面孔。真好，这象牙塔里升腾的人间烟火。<br />
 <br />
ps. 本人由于在学费政策前入的学，所以至今只缴过本学期的学费，再加上快毕业了，看学弟学妹们都年轻得不行不行的，，，，所以也没好意思去参加游行。纯打酱油。<br />
