													<h2>北京调整地铁“禁报令”  信报将移至地铁站外发放</h2>
						
						
																											<div id="postmessage_446010" class="t_msgfont">　2010年01月22日08:03&nbsp;&nbsp;来源：人民网-《京华时报》<br />
　地铁“禁报令”出台后，十几天来遭到广泛质疑。昨天，有关部门对“禁报令”进行了调整，出台了三点意见，除1、2号线以外的新地铁线有条件的站台经政府部门审批后可以卖报，在地铁出入口将新建一批报刊亭，目前仍暂时全面禁止在地铁内销售报纸，在地铁内免费发放的《北京娱乐信报》也将于近日移至地铁站外发放。<br />
<br />
　　北京地铁运营公司新闻发言人贾鹏昨天在新闻通气会上表示，2009年6月北京市通过了《北京市城市轨道交通安全运营管理办法》，根据这个被称作213号令的办法，北京地铁车厢、站台、站厅、通道都严禁擅自摆摊设点。<br />
<br />
　　贾鹏称，进入2010年，北京地铁运营量迅速攀升，北京市9条地铁线路正常工作日客流量达到500万人次，最高日客流量达到530万人次。北京地铁公司所管辖的8条地铁线路日客流量也达到430万至460万人次。因此，北京地铁所采取的措施是为了保障地铁乘客的安全。<br />
<br />
　　他同时表示，自从今年被要求严格执行213号令规定禁止在地铁内销售报纸后，地铁运营公司也注意到了社会各界的关注，同时还有很多乘客通过地铁服务热线、文明监督员等各个渠道向地铁公司表示，禁报后对自己的生活出行造成很多不便。而地铁运营公司也及时地将这些情况反映给了政府有关部门，在地铁运营公司和相关政府部门的协调下，就保证地铁运营安全的同时，如何满足乘客阅读报纸的需求提出了三点意见：第一，地铁运营部门将协调相关部门，在有条件的地铁站外，靠近出入口的地面新建一些报刊亭，方便乘客在进入地铁站前能够购买到报纸，第二，呼吁相关部门在地铁新线的规划和建设时，尽量为商业设施预留空间，对于近年新建的地铁线路，则会选择一些车站进行安全评估，对于安全达标的车站，经过政府相关部门批准后，可以进行一些商业服务，比如销售报纸；第三，对于《北京娱乐信报》，出于地铁安全运营的考虑，将尽快移出地铁站内，改在地铁站外免费发放。<br />
<br />
　　■马上就访<br />
<br />
　　新地铁线将有条件解禁<br />
<br />
　　记者：如何理解新近开通的地铁线？<br />
<br />
　　贾鹏：北京地铁1号线、2号线是出于战备考虑而建设的，地铁站台、站厅的空间都比较小，因此新近开通的地铁线可以理解为除了地铁1号线、2号线以外的其他地铁线路。<br />
<br />
　　记者：213号令并不是无条件禁止地铁站内摆摊设点，而是禁止“擅自”摆摊设点，如何理解“擅自”？经过安全评估后，地铁公司是否就可以批准相关商业服务，还是需要政府部门批准？<br />
<br />
　　贾鹏：地铁运营公司只负责地铁的安全运营服务，地铁内进行商业服务，必须要经过政府部门的批准。<br />
<br />
　　记者：具体哪些政府部门？贾鹏：目前还没有确定，但根据规定，肯定要涉及安全监督部门、交通运输主管部门以及公安部门。<br />
<br />
　　记者：发行渠道是地铁运营公司旗下的文化公司的一项重要业务，此次全面禁报后是否对其影响很大？<br />
<br />
　　贾鹏：据我了解，文化公司和一些媒体有着良好的合作关系，《北京娱乐信报》转到地上发行后也将由地铁的文化公司负责发行。<br />
<br />
　　■新闻背景<br />
<br />
　　“禁报令”引发强烈质疑<br />
<br />
　　近日，北京市委宣传部和公交总队向地铁运营公司下发通知，要求严格执行《北京市城市轨道交通安全运营管理办法》，禁止在地铁站台、站厅内销售报纸的行为，但容<br />
<br />
　　许《北京娱乐信报》作为地铁报，在地铁站厅免费发放。该项政策实施后，引起了社会各界强烈反响，被称为“地铁禁报令”。原本在地铁内发行的媒体和相关专家认为，此举<br />
<br />
　　“只许州官放火，不许百姓点灯”，有违市场经济公平竞争原则；还有大量乘客也抱怨，禁报令一出，影响了自己在地铁内看报的习惯，给出行生活带来不便。（记者郭鲲）</div>

							
							
							
							
