　　本报讯 昨日新京报网调查显示，对于日前出台的地铁“禁报令”，多数受访网友持不同意见。78.33%的受访者认为，应在地铁设固定书报亭，进行统一管理。仅有8.43%的受访者赞成“直接取缔，一刀切”。<br />
<br />
　　地铁“禁报令”规定，地铁站内禁止发售报纸，仅有《北京娱乐信报》可免费发放。对此，新京报网昨日进行了调查，截至昨日22时，共有1020位网友参与投票。63.04%的参与者有在地铁读报的习惯。对于有关部门在“禁报令”中提出的安全因素，超八成（81.08%）受调查者称从未遇到因买报纸发生过拥挤等危险状况。<br />
<br />
　　对于地铁站内卖报的管理，仅8.43%的受访者认为应“直接取缔，一刀切”。78.33%的受访者建议“设固定书报亭，统一管理”，40.88%的受访者认为应“对售报人员进行培训”，27.35%的受访者认为可以考虑“设自动售报机”。<br />
<br />
　　■ 现场探访<br />
<br />
　　“站外买报绕道费时间”<br />
<br />
　　时间：1月11日下午 地点：地铁2号线东直门站<br />
<br />
　　昨天下午，2号线东直门站的站台上已经买不到报纸。该站一工作人员表示，以前站台上南北两头分别设有一个定点报摊。<br />
<br />
　　“要买报纸，明天早上再来吧”，现场安保人员表示，从元旦假期结束，站内已经不准摆报摊，但早高峰时期有人流动卖报，会卖到9点多。<br />
<br />
　　北京取缔地铁站内售报让许多乘客不解。乘客们表示，现在站内买不到报纸很不方便。只能从地铁口附近的报刊亭买好报纸再入地铁，有时还要绕路，很浪费时间。<br />
<br />
　　家住建国门附近的周先生说，他的单位在东直门附近，10几年来，每天都是乘坐地铁上下班，在地铁上看报已经成为一种习惯，就像每天要吃饭睡觉一样，不可或缺。<br />
<br />
　　“把报摊设在站内不碍事儿的地方就可以了”，几位地铁乘客表示，不觉得站内设报摊有多大的影响。<br />
<br />
　　东直门地铁站安保人员称，本来摊位就是定点设在靠柱边的，很少因为报摊造成乘客拥挤的情况。<br />
<br />
　　站内难买报 乘客称不便<br />
<br />
　　时间：1月11日 地点：2号线建国门站、1号线东单站等<br />
<br />
　　昨日，地铁1号线、2号线、5号线多位工作人员表示，禁报令实施以后，均未见到站内摆报摊。昨日15时许，建国门开往朝阳门的地铁2号线上，两个乞丐正穿梭卖唱乞讨，三节车厢内只有4位乘客读报。<br />
<br />
　　乘客樊达计算，从住处上地到最近的报刊亭买报，步行需要7分钟，而在地铁站内购报不过10秒钟。他说，地铁“禁报令”给很多读者带来不便。<br />
<br />
　　多位读者称，他们在媒体上从未听说，地铁站内发生安全突发事件和报摊有关系，相反，很多售报员还是地铁站的安全监督员，在工作人员人手不够时，他们还会帮着监督秩序。<br />
<br />
　　“其实（报摊）对站内秩序没什么影响，但上级这么要求，我们就得照办。”建国门地铁站一位工作人员说。而磁器口站两位地铁保安表示，虽然没听说地铁安全突发事件与报刊摊有关，但禁令可能也是防患于未然。<br />
<br />
　　■ 专家观点<br />
<br />
　　“禁报令涉嫌强制介入竞争”<br />
<br />
　　专家称站内卖报未违背公共利益，无理由禁止<br />
<br />
　　“如果地铁站内禁止销售报刊，那么是不是铁路也应该禁止？其他交通工具也会禁止？”昨晚，中国人民大学教授张鸣接受本报采访时表示，地铁站内销售报刊，满足乘客阅读需要，这已经是“国际惯例”，“可是，没有哪个城市的地铁，因为销售刊物，发生过安全问题。”<br />
<br />
　　他认为，只允许信报一家报纸进入，其他报刊一律禁售，这相当于国家机关通过行政强制力，封闭了一个市场推广渠道，“有什么权借助行政强制力介入行业竞争？”<br />
<br />
　　中国政法大学教授、中国法学会行政法研究会常务理事刘莘则表示，报刊在地铁站内发行，既没有违背公共利益，更没有有碍公共安全，“公众无法为此找到一个充足的理由，所以会问，理由何在？”<br />
<br />
　　刘莘表示，这道“禁止性命令”，却给予了《信报》一个特殊垄断地位―――在京城地铁站内垄断发行的地位，“这难免引发猜测，是不是背后有什么利益？政府部门到底是为了什么？”<br />
<br />
　　■ 委员声音<br />
<br />
　　“赠报易拥挤，卖报较有序”<br />
<br />
　　市政协委员高春锦称看书读报文化不应消失<br />
<br />
　　高春锦是北京市政协委员，2009年初，他曾通过提案，给地铁站内免费赠阅的报纸提了点管理意见。<br />
<br />
　　近一年过去后，高春锦委员仍认为，在地铁站内免费赠报有些不妥。他说，免费赠阅的报纸通常在早高峰时期发放，此时客流量正多。由于免费赠阅，几乎所有经过发放点的乘客都会去领报纸，一些地铁站会出现乘客拥挤着等报纸的现象。由于赠报通常在站台楼梯上方，如果部分乘客抢时间上车，有可能出现拥挤状况。而在地铁站内买报纸的乘客数量相对少，比较有序。<br />
<br />
　　对于保留可能带来安全隐患的免费报纸发放，却取消比较有序的卖报行为，高春锦表示不解。他说，在地铁车厢内看书读报是一种文化，不应该在北京消失。<br />
<br />
　　本版采写/本报记者 左林 王姝 刘泽宁 李天宇 傅沙沙 实习生 喻亚</div>

							
							
							
							
