　　<br />
　　北京市地铁运营公司：<br />
　　<br />
　　2009年12月1日，中共北京市委宣传部召开会商会，对轨道交通内报刊发售问题进行了专题研究，会后下发了《轨道交通站内报刊发售问题会商会会议纪要》。《纪要》中明确指出，除信报外，任何单位和个人停止一切在轨道交通站内报刊销售行为，公安机关严格依法取缔在轨道交通内的报刊发售行为。但是，近期公交总队和各级领导在对轨道交通的安全检查中发现，在多处车站站台两侧仍有发售报刊的问题。现就轨道交通站内信报发放和停止销售其他报刊的意见函告如下：<br />
　　<br />
　　一、《北京娱乐信报》在轨道交通站内免费发放问题：<br />
　　<br />
　　根据《轨道交通站内报刊发售问题会商会会议纪要》，《北京娱乐信报》作为市委宣传部批准的北京唯一的地铁报是服务宣传的重要阵地，可以在保证不影响地铁安全秩序的情况下免费发放，但信报的发放地点及位置需选在空间大，便于客流疏散的地铁站指定位置，不得进入站台。<br />
　　<br />
　　二、除信报外，其他报刊的发售问题。<br />
　　<br />
　　根据新修改的《北京市城市轨道交通安全运管管理办法》和中共北京市委宣传部《轨道交通站内报刊发售问题会商会会议纪要》关于“除信报外，任何单位和个人停止一切在轨道交通站内报刊销售行为，公安机关严格依法取缔在轨道交通内的报刊发售行为”精神，请地铁运管公司对轨道交通内的设摊售报问题进行研究，尽快停止轨道交通内除信报外的其他报刊的销售。<br />
　　<br />
　　根据上级领导指示，公交总队已下发通知，要求各派出所严格按照会议纪要和政府令的要求，一是监督信报在指定的地点发放，严格禁止信报进入站厅站台发放；二是对在轨道交通内的报摊销售行为进行清理，望贵公司对此项工作给予支持。<br />
