　　(1904～1942年)，又名戴炳阳，原名衍功，自号海鸥。汉族，安徽无为练溪乡旗杆戴村人。著名民族英雄，抗日名将。黄埔系骨干将领。<br />
生平事迹年表<br />
　　1904年11月25日出生在安徽省无为县。<br />
　　1923年考入陶行知先生创办的安徽公学高中部。<br />
　　1924年投奔国民革命军。<br />
　　1926年黄埔军校第三期毕业，历任国民党军队排长、连长、营长、团长。<br />
　　1926年参加北伐。<br />
　　1933年3月，率部参加长城古北口抗战，荣获五等云麾勋章。<br />
　　1937年8月升任第25师73旅旅长。<br />
　　1938年3月，在台儿庄战役中，戴旅火攻陶墩，智取朱庄，激战郭里集，迫使台儿庄之敌后撤，得华胄勋章(一说宝鼎勋章)1枚。<br />
　　1938年5月，在徐州会战中，曾率部在中艾山与日军激战4昼夜，因战功卓著，升任第89师副师长兼第31集团军总部干训班教育长。<br />
　　1938年8月，率部投入武汉会战，被第31集团军记大功1次。<br />
　　1939年1月5日升任第200师师长，接替杜聿明。该师是新建的第五军的主力师。<br />
　　1939年5月，率部参加抗击日军进犯的随(县)枣(阳)之战。<br />
　　1939年9月，参加长沙保卫战。<br />
　　1939年11月，参加桂南昆仑关战役。<br />
　　1940年1月，在坚守昆仑关的战斗中，戴部确保441高地，毙敌百余人，毁敌坦克2辆、炮4门，缴获枪械百余支。11日，戴安澜身负重伤。国民党政府颁授四等宝鼎勋章(一说青天白日勋章)1枚嘉奖之。<br />
