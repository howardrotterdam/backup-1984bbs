																											<div id="postmessage_224628" class="t_msgfont">明报／「所有的生离死别，都发生在某一个码头，上了船，就是一生」！1949年，是改变了全球华人命运的年代，两岸数以百万计的人心中藏不同的「记忆黑盒子」，从未对彼此打开过。<br />
<br />
酝酿10年、行走万里、闭关400天、跨越五大洲，寻访1949年亲历国共内战无名人物的亲身经历，台港著名女作家龙应台新书《大江大海一九四九》昨天出版，新书颠覆了60年来两岸以军事史、政治史、国家领袖为核心的叙述角度，是叙述「你所不知道的1949」。<br />
<br />
以身为失败者后代为荣<br />
<br />
对大陆正举国动员热烈庆祝建政60年，龙应台别有所感地说，「战争，有所谓的胜利者吗」？她希望所有死在战场上的人都能入土为安，要由前人的悲惨历史中深切认知战争的悲痛，这不是解放军与国军的胜败，是「国家的悲剧」，她以身为「失败者的下一代为荣」，因为军事上的「失败者」却在往后的60年中，在台湾建立起新的价值观，她也希望国家主席Hu Jintao在建政60周年庆典上说的第一句是「对不起」，要有对土地的疼爱，而不是只有庆祝的典礼。<br />
<br />
昨天在台北举行的新书发表会上，请来了南太平洋岛国巴布亚新几内亚拉包尔战俘营存的90岁高龄游击队长李维恂、参与接收台湾的国军70军军人林精武、12岁就成了「少年兵」的桑品载、加入国军转而成为解放军的台湾原住民陈清山、亲身经历济南城破的卢雪芳等人，现场也播放了这几位历史见证者的亲身访谈纪录片段。<br />
<br />
朱经武父亲与龙父早结缘<br />
<br />
香港科技大学前校长朱经武昨天也出席新书记者会，他谈到其父与龙应台父亲在1949年的一段因缘。当年朱父是国军少尉，押送几箱黄金要上船，没想到在广州天河机场时黄金箱子掉了一地，被驻地宪兵给拦走，而龙应台父亲正好是天河机场的宪兵，还在自传中提到这段经历，龙应台笑说，「我爸爸抢了朱经武父亲好几箱黄金」。<br />
<br />
「我带深情写这本书」，龙应台说，写作的动机是想用文学的方式，对她父母亲这一整代人做一个致敬跟告别，「向所有被时代践踏、侮辱、伤害的人致敬」，因为所有的伤痛一定要有温柔治疗，即使60年也不晚。</div>

							
							
							
							
