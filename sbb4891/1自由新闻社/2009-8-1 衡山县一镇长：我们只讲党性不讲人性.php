																											<div id="postmessage_235597" class="t_msgfont">周建国镇长:“我们只讲党性不讲人性，拆”<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;“我们只讲党性不讲人性，拆！”顷刻几百村民露宿田野<br />
&nbsp; &nbsp;&nbsp; &nbsp;7月23日下午，笔者在湖南省衡山县店门镇桂花村田野，看到惊人的场景，100亩田野稀稀拉拉的搭着几十个棚子，好象刚刚发生了特大地震似的，几百村民生活、露宿田野。<br />
&nbsp; &nbsp;&nbsp;&nbsp;经了解，出现这个场景，源于店门镇周建国镇长的一句“我们只讲党性不讲人性，拆” 的指令，顷刻之间，4台挖掘机强行推倒了50多户村民的住宅，致使几百村民生活、露宿在没有任何遮挡的田野成了“灾民”。在这批村民中，年长者80多岁，年小者才两个月。村民戏谑地说：“我们比汶川地震灾民还苦啊，因为他们遭受的是自然灾害，有政府和四方人民支援，我们是人为‘灾害’，没有任何人会理落我们。”<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;据了解，衡阳市决定修筑衡阳至南岳的高速公路，公路经衡山县店门镇桂花村康家湾、万工桥两组。康家湾组26户108人，25户拆；万工桥组37户200多人，26户拆，两个组共拆迁51户房屋。2009年4月24日，衡阳县出台 “关于衡岳高速公路（衡山段）征地拆迁安置方案。”<br />
&nbsp; &nbsp;&nbsp;&nbsp;6月25日，“衡岳高速公路建设协调领导小组办公室”与村民签订拆迁协议，拆迁价每平方最高的450元，最低135元，建房地基指令在各自耕种的农田里。村民顾全大局，虽然对“办公室”一口价有些不好接受，但都认可了，只是对将几十户房屋都建在“吨粮田”里的指令提出了意见，认为这不符合省、市、县“原则使用荒山荒地、空坪、隙地，一般不占用耕地”的指示，加之康家湾组只有76亩耕地，这次修路又征去36亩，拆迁建房又要占去21亩，剩下的耕地人均只有1分多，全组出现严重耕地不足，因此，村民强烈要求将房屋建在离高速公路安全范围外的山坡上，这样可节约大量良田，又便利了拆迁户建房。<br />
&nbsp; &nbsp;&nbsp;&nbsp;店门镇领导对村民的要求以拒绝，并称“宁愿毁100亩良田也不能同意你们山坡建房的要求”。并且店门镇政府于2009年7月8日下达了盖半边公章的“限期拆迁通知书”。限“7月15日前必需搬迁完毕，否则强行拆除。”<br />
&nbsp; &nbsp;&nbsp;&nbsp;6月底7月初正是当地收割席草（当地特产经济作物）、早稻和栽种晚稻的大忙季节；加之6月25日签订协议到7月15日前搬迁，仅20天时间，无法建起新房，搬迁没有地方安身而误了些时间.<br />
&nbsp; &nbsp;&nbsp;&nbsp;这就激怒了镇政府，7月14日下午，由镇长周建国带领镇干部、镇派出所、镇武装部干部和4台挖掘机，在“我们只讲党性不讲人性，拆”的大声命令下，4台挖掘机轰隆开过，村民的住房顷刻倒塌，许多村民虽然通宵抢搬家俱财物等，终因人力不胜机器，许多财物被砸毁、被掩埋。至7月15日上午，几百村民不论老幼，通通生活、露宿在气温高达36、7度的田野里，至7月23日下午，虽然过去8天了，笔者还见到一些村民生活在毫无遮挡的田野、路边。<br />
&nbsp; &nbsp; 50多户拆迁村民中，康水金一户杂屋在高速公路红线内，正房不在高速公路红线内，康的妻子强烈要求不要拆他家的正房而拖延了一天，7月16日，在副镇长的指挥下，这户正房不在红线内的房子也被拆除了，这位副镇长十分得意地说：“谁能斗赢共产党？！” <br />
