													<h2>胡说：领导干部应树立制度面前没有特权意识</h2>
						
						
																											<div id="postmessage_428749" class="t_msgfont">胡锦涛：领导干部应树立制度面前没有特权意识<br />
<br />
2010年01月13日08:00<br />
<br />
<img src="http://img.hexun.com/2010-01-13/122340285.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　胡锦涛在十七届中央纪委五次全会上发表重要讲话强调<br />
<br />
　　人民网北京1月12日电中共中央总书记胡锦涛1月12日在中国共产党第十七届中央纪律检查委员会第五次全体会议上发表重要讲话。他强调,全党必须正确认识党风廉政建设和反腐败斗争形势,既充分看到反腐倡廉建设已经取得的显著成效,又深刻认识反腐败斗争的长期性、复杂性、艰巨性,以更加坚定的信心、更加坚决的态度、更加有力的措施、更加扎实的工作,坚定不移把党风廉政建设和反腐败斗争推向前进。<br />
<br />
　　中共中央政治局常委吴邦国、温家宝、贾庆林、李长春、习近平、李克强、周永康出席会议,中共中央政治局常委、中央纪律检查委员会书记贺国强主持会议。<br />
<br />
　　胡锦涛在讲话中指出,2009年,各级党委、政府和纪检监察机关全面推进反腐倡廉建设,加强对中央保增长、保民生、保稳定等一系列决策部署贯彻落实情况的监督检查,严肃查处一批违纪违法案件,坚决纠正损害群众利益的不正之风,扎实推进一批改革措施,党风廉政建设和反腐败斗争方向更加明确、思路更加清晰、措施更加有力,为改革发展稳定提供了有力保证。<br />
<br />
　　胡锦涛强调,在新的一年里,我们必须全面贯彻党的十七大和十七届三中、四中全会精神,高举中国特色社会主义伟大旗帜,以邓小平理论和“三个代表”重要思想为指导,深入贯彻落实科学发展观,坚持标本兼治、综合治理、惩防并举、注重预防的方针,加强以保持党同人民群众血肉联系为重点的作风建设,加强以完善惩治和预防腐败体系为重点的反腐倡廉建设,抓紧解决反腐倡廉建设中人民群众反映强烈的突出问题,着力推进反腐倡廉制度建设,围绕中心、服务大局, 开拓创新、狠抓落实,不断取得党风廉政建设和反腐败斗争新成效。<br />
<br />
　　胡锦涛指出,当前,要重点抓好以下几方面工作。第一,加强对中央关于推动科学发展重大决策部署执行情况的监督检查,及时发现问题、督促整改。第二,加强对领导干部的教育监督,以党性党风党纪教育为重点,加强对党员干部特别是领导干部的理想信念教育和廉洁从政教育,大力弘扬党的优良作风,继续引导领导干部讲党性、重品行、作表率。第三,认真解决损害群众利益的问题和群众反映强烈的突出问题,严肃查处严重侵害群众利益案件,促进社会和谐稳定。第四,坚决查处违纪违法案件,严肃查处发生在领导机关和领导干部中滥用职权、贪污贿赂、腐化堕落、失职渎职案件,决不让任何腐败分子逃脱党纪国法惩处。<br />
<br />
　　胡锦涛强调,反腐倡廉制度建设是惩治和预防腐败体系建设的重要内容,是加强反腐倡廉建设的紧迫任务。要以建立健全惩治和预防腐败体系各项制度为重点,以制约和监督权力为核心,以提高制度执行力为抓手,加强整体规划,抓紧重点突破,逐步建成内容科学、程序严密、配套完备、有效管用的反腐倡廉制度体系,切实提高制度执行力、增强制度实效。<br />
<br />
　　胡锦涛指出,要突出抓好以下4个方面工作。一是要进一步加强反腐倡廉教育制度建设,加强和改进教育培训工作,加强和改进反腐倡廉宣传教育工作, 提高反腐倡廉教育的科学性、规范性、有效性。二是要进一步加强监督制度建设,认真执行和不断完善各项监督制度,改革和完善党内监督体制,健全权力运行监控机制,拓宽监督渠道,增强监督合力,加大监督制度创新力度,建立健全决策权、执行权、监督权既相互制约又相互协调的权力结构和运行机制。三是要进一步加强预防制度建设,推进廉政风险防控机制建设,建立健全预防腐败信息系统,建立健全防止利益冲突制度,形成有效预防腐败的长效机制。四是要进一步加强惩治制度建设,建立健全腐败案件及时揭露、发现、查处机制,建立腐败现象易发多发领域调查分析和专项治理制度,健全党风廉政建设责任制及其配套制度。<br />
<br />
　　胡锦涛强调,形势在发展,实践在深入,人民群众对反腐倡廉提出了新要求新期待,这些都要求我们及时研究新情况、解决新问题、总结新经验,推进反腐倡廉制度建设创新。要深入研究反腐倡廉建设的突出问题,按照与改革开放伟大实践相适应、重大举措相配套的要求,配合深化行政管理体制改革、干部人事制度改革、司法体制和工作机制改革来进行。<br />
<br />
　　胡锦涛指出,切实抓好反腐倡廉制度建设,必须不断提高制度执行力。各级党委、政府和纪检监察机关要切实加强制度执行的组织领导,保证反腐倡廉各项制度得到切实执行。要切实加强制度宣传教育,增强全党制度意识,筑牢遵纪守法思想基础,把制度转化为党员、干部的行为准则、自觉行动。要切实加强制度执行情况监督检查,切实查处违反制度的行为,着力在领导干部特别是高中级干部中树立法律面前人人平等、制度面前没有特权、制度约束没有例外的意识,教育引导领导干部带头学习制度、严格执行制度、自觉维护制度。<br />
<br />
　　胡锦涛强调,纪检监察干部要注重加强思想政治建设和能力建设,增强政治意识、表率意识、法治意识、创新意识、宗旨意识,忠诚于党和人民事业,坚持党性原则,忠实履行职责,带头遵守和维护党纪政纪国法,对腐败分子和消极腐败现象进行坚决斗争。各级党委和政府要高度重视并自觉承担起推进反腐倡廉建设的政治责任和领导责任,加强对纪检监察工作的指导,支持纪检监察机关履行职责、开展工作,帮助解决纪检监察机关遇到的实际困难和问题,推动落实反腐倡廉各项任务。<br />
<br />
　　贺国强在主持会议时指出,胡锦涛总书记的重要讲话,从党和国家事业发展全局和战略的高度,全面、科学地分析了当前的反腐倡廉形势,明确提出了今年党风廉政建设和反腐败工作的总体要求和主要任务,着重阐述了加强反腐倡廉制度建设的重要性、紧迫性和基本要求,对于进一步统一思想认识,坚定工作信心, 深入开展党风廉政建设和反腐败斗争,加强和改进新形势下党的建设,具有重大而深远的意义。我们一定要认真学习领会,坚决贯彻落实,以党风廉政建设和反腐败斗争新成效,为推动科学发展、促进社会和谐、开创新形势下党的建设新局面提供坚强有力保证。<br />
<br />
