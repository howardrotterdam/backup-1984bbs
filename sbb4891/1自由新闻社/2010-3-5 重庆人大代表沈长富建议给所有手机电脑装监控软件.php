													<h2>重庆人大代表沈长富建议给所有手机电脑装监控软件</h2>
						
						
																											<div id="postmessage_503652" class="t_msgfont"><a href="http://news.qq.com/a/20100305/002825.htm" target="_blank">http://news.qq.com/a/20100305/002825.htm</a><br />
本报讯 (特派北京记者 杨涛)给电脑、手机都装上监控软件？网络游戏等分时段开放？这是全国人大代表、中国移动重庆公司总经理沈长富在今年两会的议案《关于加强网络规范管理，构建健康网络环境的建议》中提出的建议。<br />
<br />
南方都市报两会“说吧”昨晚10时将这条议案在微博互动平台公布，引发网友热议，一边倒反对，有网友直言此议案侵犯通信自由和通信秘密，涉嫌违宪。<br />
<br />
向“濉薄”、MLGB宣战<br />
<br />
沈长富认为“网络发展带来的问题日趋突出”，不良信息的传播如蛆附骨，“由于个人意识的极度膨胀”，网民滥用舆论监督功能宣泄个人情感误导社会，网络文化病态化、庸俗化发展扰乱社会秩序。<br />
<br />
谈到网络文化病态化、庸俗化，沈对网络流行语表达了强烈不满。“一些网络流行语被频繁使用，如M LG B是骂人的粗话；一些生僻字也‘推陈出新’，如‘濉、‘’等。这些流行语失去了文字、语言本身应具有的意境、更谈不上文学价值，纯属无厘头、矫揉造作。”<br />
<br />
织“罗网”保护网民安全<br />
<br />
沈长富建议及时建立网络监察队及网民举报机制，对查清的网络犯罪加以处罚。公安机关可进行定期网络巡查与不定期抽查，维护良好的网络秩序；可采取强行关闭违规网站，拘留犯罪嫌疑人等。<br />
<br />
沈长富认为，规范网络管理，有必要在所有环节都织起一张“罗网”，将所有隐藏着不良行为的非法信息都过滤在外，保护网民的安全。<br />
<br />
沈长富建议在网络的所有环节都织起一张“罗网”。他说，网络的实质即是信息，网络包含信息内容、信息载体、信息通道平台、信息接收终端等一系列组合。规范网络管理，有必要在所有环节都织起一张“罗网”，将所有隐藏着不良行为的非法信息都过滤在外，保护网民的安全。<br />
<br />
“罗网”具体措施<br />
<br />
网络用语分级制<br />
<br />
加强对发布内容的审查、过滤，如设立违规信息纠察、举报机制以及网络用语分级机制等，对网络用语按一定标准区分为限制级、禁用级等，有的不良用语可以直接屏蔽。<br />
<br />
传播渠道“分级”<br />
<br />
根据参与信息传导的渠道在信息传导中的作用不同而分级，进而要求其承担不同程度的监管责任。<br />
<br />
电脑手机终端装“识别软件”<br />
<br />
议案中提出“我们可以考虑加强对信息终端的规范，对终端(如电脑、手机)装备具有一定识别功能的软件。”<br />
<br />
网络娱乐“分时段”<br />
<br />
应对部分网络娱乐等实行分时段开放的方式，既可丰富网民的业余生活，同时约束网民，及时遏制沉迷网游的不良之风。<br />
<br />
代表回应<br />
<br />
沈长富：装软件只限制黄色<br />
<br />
他认为学生上课时间可以关闭网游<br />
<br />
本报讯特约《京华时报》记者刘薇昨晚，沈长富就他的议案接受记者采访。他说，自己作为运营商，在网络扫黄打非的背景下，有义务提出相关建议，“我的出发点很简单”。至于网游限时问题，他说，自己认为学生上课时间可以关闭网游，而寒暑假可以不限，“还没有很细化的想法”。关于网友指出他提案监控电脑、手机，沈长富称，自己的建议是在电脑和手机上装软件，限制黄色等内容，并没有所谓监控他人隐私的想法。<br />
<br />
最后，沈长富表示，自己将从此事中吸取教训，今后再提议案会很谨慎，深思熟虑之后再提出。<br />
<br />
“说吧”微博互动<br />
<br />
代表，请学好宪法再来<br />
<br />
昨晚10时，南都两会“说吧”将沈长富议案的部分内容在微博发布，征求网友意见，截稿时收到评论85条，没有一条赞成的。<br />
