<br />
　　一名13岁少女在溜冰场玩耍时被一个大哥哥“英雄救美”，少女为此喜欢上他。在少女追求下，两人建立恋爱关系，并发展到同居。昨天，潼南县检察院透露，救美大哥因明知少女只有13岁仍与之发生性关系，涉嫌强奸，遂对其批捕。目前，案件还在进一步侦办中。<br />
<br />
　　少女恋上救美大哥哥<br />
<br />
　　承办检察官透露，从户籍资料显示，赵川今年26岁。但他个头不大，看起来只有20岁。<br />
<br />
　　据赵川交代，今年7月份，他在潼南一家溜冰场滑冰时，有几个小青年欺负一个女孩，他看不过去，就上前解了围。这样，他就和女孩认识了。女孩说她叫雷雨，是中学生。当晚，他请雷雨吃了晚饭，还互留电话，然后各自回家。<br />
　　雷雨告诉民警，自从他们认识后，两人经常煲电话粥和见面。接触多了，她就喜欢上赵川，并多次提出要与他耍朋友，赵最后答应了。8月中旬的一天晚上，她和赵川在外面约会时，两人都有点激动。于是，就在潼南的体育馆旁边一个旅馆开房，然后发生了第一次性关系。<br />
　　雷雨与赵川恋爱后，时常出双入对出现在街上。这事很快传到雷雨父母耳边。<br />
　　雷雨的母亲称，她知道女儿早恋后，坚决反对这件事，还曾三次将女儿反锁在家里，不准其出门。为了防止女儿逃跑，她甚至还守在家里。但女儿都想方设法逃了出去。<br />
<br />
　　雷雨称，赵川知道她父母反对他们的事情后，就到成都打工。8月下旬，她给赵川打电话，说要跟他一起去，赵就喊她到成都找他。8月22日，她到成都见到赵，赵说他要到昆明去打工。于是，他们当天就从成都启程前往昆明。<br />
<br />
　　知道女友13岁仍同居<br />
<br />
　　到了昆明后，赵川在昆明找了一份水电工的工作。然后，他和雷雨以恋爱关系同居在一起。9月上旬一天，赵觉得女友这样没事做既无聊又没钱，而且两人的花销仅靠他一人打工挣钱，日子有点不好过，遂提议让雷雨去找个工作。<br />
<br />
　　赵川称，雷雨一听说要出去打工，才说了真话。说她只有13岁，没有身份证，没有哪个老板会用她。而在此前，他从没看见过雷的身份证，不知道雷的实际年龄。他为此问过几次，雷有时说她17岁，有时又说16岁。结合雷的外表，他相信雷至少已经16岁了。<br />
<br />
　　赵川还称，他听说雷雨只有13岁时，就想到这事是犯法的，但又考虑到他既然已经同雷发生了性关系，就要同她过一辈子，要对雷负责到底，所以才一直保持着同居关系。后来通过再三考虑，他决定从昆明回潼南解决这个问题。回到潼南后，他和雷雨首先回了一趟他的家，向父母表明他和雷的关系。之后又到雷家，表明了他们的关系。雷的父母没有反对他们，并让他们同居在一个房间达20天之久。<br />
<br />
　　检察官称，雷雨个头有点高，单从外表看，与她的实际年龄有点不相符。这与现在生活水平提高有一定关系。<br />
<br />
　　母亲带着女儿去报案<br />
<br />
　　雷雨的家长告诉办案人员，开始的时候，他们非常反对女儿谈恋爱，但后来看他们已经同居在一起，并有了那种关系，也就没怎么反对了。<br />
<br />
　　但后来，雷雨母亲觉得女儿毕竟才13岁，不能就这样毁了，遂在10月18日带着女儿和赵川到派出所报案，称13岁的女儿和男友恋爱同居并多次发生性关系。警方为此以赵川涉嫌强奸立案，检察机关也以涉嫌强奸罪对其批捕。<br />
<br />
　　当办案人员问雷雨有什么想法时，她说他们是自愿在一起的，没有要求追究赵川任何责任的想法，希望公安机关能够对赵宽大处理。（涉案当事人均为化名）<br />
<br />
　　男方为何涉嫌强奸？<br />
<br />
　　两人自愿恋爱并自愿发生性关系，男方为何涉嫌强奸？<br />
<br />
　　潼南县检察院李检察官为此解读了有关法律规定。李检察官称，根据最高人民法院、最高人民检察院、公安部《关于当前办理强奸案件中具体应用法律的若干问题的解答》规定，奸淫幼女是指与不满14周岁的幼女发生性的行为。该行为不论行为人采用什么手段，也不问幼女是否同意，只要与幼女发生了性行为，就构成犯罪。同时，根据最高人民法院《关于行为人不明知是不满14周岁的幼女，双方自愿发生性关系是否构成强奸罪问题的批复》规定，行为人明知是不满14周岁的幼女而与其性关系，不论幼女是否自愿，均应以强奸罪定罪处罚。<br />
<br />
　　但还有一种特殊情况可以不认为是犯罪。李检察官称，那就是批复规定的，行为人确实不知对方是不满14周岁的幼女，双方自愿发生性关系，未造成严重后果，情节显著轻微的。而本案赵川的行为可以划分为两个阶段。一是赵让雷去找工作前，他不知她实际年龄。二是在此后，赵知道了雷未满14岁。但赵在后一阶段仍然多次与雷发生性关系。所以，赵已涉嫌强奸罪。<br />
