													<h2>周强升任湖南一把手 传李小鹏将接任省长</h2>
						
						
																											<div id="postmessage_583426" class="t_msgfont">中共中央过去两天里，连续宣布两项重要人事任命。继前天宣布将原湖南省委书记张春贤调任为新疆维吾尔自治区党委书记以后，不出所料，中央昨天任命现任湖南省长周强接替张春贤留下的空缺，升任为湖南省委书记。与此同时，昨天也有消息指原中国总理李鹏之子、现任山西副省长李小鹏将随后调往湖南，接替周强的省长位置。<br />
<br />
　　现年50岁的周强与现年51岁的李小鹏，都是1960年代左右出生的中国政坛新星。周强出身共青团系，曾在团中央工作长达11年，其中八年为团中央第一书记；身为前国家领导人之后的李小鹏则是在2008年由商界转入政坛，依循其父步履走上政治舞台中心。上述人事动向的信息，显示中共“十八大”前的人事布局、不同派别与出身背景官员的卡位动作已在加速进行。<br />
<br />
　　在中央前天宣布张春贤接替原新疆书记王乐泉职务时，已有分析指出此举为周强的升迁创造了条件。周强是湖北人，文革结束后于1978年考入西南政法大学，1985年硕士研究生毕业后长期在司法部工作，1995年由司法部调到共青团中央任职，三年后，以38岁之龄当选团中央第一书记，成为耀眼的年轻正部级政治明星。<br />
<br />
　　2006年，他循现任中共总书记、国家主席胡锦涛，现任中央政治局常委、国务院副总理李克强模式，从团中央调任地方大员，成为湖南省代省长，隔年正式当选为省长，当时他46岁，是中国最年轻的省长。<br />
<br />
　　不过，周强到湖南以后的一段时间里，外界未闻其动向。他在省长位阶上一呆四年，如今上位为省委书记，他的锋芒已不及比他更年轻、同为团系出身的现任内蒙古自治区委书记胡春华（现年47岁），或者从北京走上政坛，曾为北京市委秘书长，协助市委书记刘淇工作的现任吉林省委书记孙政才（现年47岁）。<br />
<br />
　　昨天，香港《明报》与路透社都发出消息，指周强的接任者据传为李小鹏。李小鹏原为国企华能集团董事长（副部级），2008年弃商从政，他的首个政坛职务是山西省副省长，上个月又获升为山西省常务副省长，在副省长中排名第二。<br />
<br />
团系出身政坛明星<br />
<br />
　　一般认为，李小鹏与周强两人仕途看涨。经这次升迁后，团系出身的周强继续被归入胡春华、孙政才、陆昊（现任团中央第一书记）、张庆伟等年轻政坛明星序列里，成为有望进入中央领导层的人选之一。今年51岁的李小鹏在年龄上稍处劣势，但是从他升迁的速度与趋势观察，不能排除他更上层楼的可能。<br />
<br />
　　今年43岁的陆昊，也是目前备受关注的中国“60后”高官。陆昊是上海人，拥有经济学硕士学位，他从管理企业开始，以后担任了北京关村科技园区管委会副主任、主任、党组书记、海淀区委副书记，北京市副市长、市政府党组成员。据悉，陆昊的两次重要升迁都与时任北京市委书记的贾庆林关系密切，他因此被认为是贾庆林所属的“江系人马”，但是他在2008年从北京副市长职位上空降入团，出任团中央第一书记，身兼两种派别背景是陆昊继续升迁的重要政治资源与有利条件。<br />
目前尚未进入政坛，但是值得关注的另一名正部级资历人才，是中国商用飞机有限责任公司董事长、党委书记张庆伟。现年49岁的张庆伟毕业自西北工业大学，曾担任中国航天部高级工程师、主任设计师等职。<br />
　　<br />
&nbsp; &nbsp; 张庆伟在1992年至2007年间，担任了航空航天部、中国航天工业总公司一院一部921工程运载火箭副总设计师、副主任；中国航天工业总公司一院副院长、总经理技术业务助理兼一院副院长；中国航天科技集团公司副总经理、总经理、党组书记等职务。他在2007年被任命为国防科学技术工业委员会主任（正部级），隔年出掌中国商用飞机有限责任公司，负责国产“大飞机”项目。<br />
<br />
　　目前仍相对低调的张庆伟被视为第六代中央领导层一名不可忽视的热门人选。而且，作为国家重点项目“大飞机”计划的推进与实现，势必为张庆伟他日走上仕途提供很大助力。</div>

							
							
							
							
