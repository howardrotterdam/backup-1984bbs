													<h2>团中央任职11年，周强上任湖南省委书记</h2>
						
						
																											<div id="postmessage_583448" class="t_msgfont"><a href="http://gcontent.nddaily.com/0/3f/03f5446139179452/Blog/435/f732a4.html" target="_blank">http://gcontent.nddaily.com/0/3f ... log/435/f732a4.html</a><br />
<br />
 <br />
&nbsp; &nbsp; 中组部常务副部长沈跃跃评价周强：<br />
&nbsp; &nbsp; 他组织领导和宏观驾驭能力比较强<br />
<br />
&nbsp; &nbsp; 据人民网报道，昨天湖南省委在长沙召开的领导干部会议上，中组部常务副部长沈跃跃充分肯定了周强同志。她说，周强同志组织领导和宏观驾驭能力比较强，有改革创新精神，在政府工作中注重民主决策、科学决策，着力建设服务政府、法治政府、责任政府、廉洁政府。<br />
&nbsp; &nbsp; 中国省部级干部中，生于上世纪60年代的人逐渐增多；截至目前，在地方工作的有内蒙古自治区党委书记胡春华、吉林省委书记孙政才、湖南省委书记周强、新疆维吾尔自治区主席努尔・白克力等4人。<br />
&nbsp; &nbsp; 随着周强履新，努尔・白克力则成为目前省市区行政首长中唯一的“60后”。<br />
&nbsp; &nbsp; 获悉周强出任湖南省委书记之后，西南政法大学(原西南政法学院)官方网站在第一时间刊发了《周强校友出任湖南省委书记》的消息。<br />
&nbsp; &nbsp; 西政78级从政典范<br />
&nbsp; &nbsp; 在当代中国的政界、法学界和司法界，“西政现象”颇受瞩目，特指偏居西南重镇重庆的西南政法大学，有多名毕业生或担任高官要职，或成为法学名家；其中又以1978级的毕业生为最。<br />
&nbsp; &nbsp; 据南方都市报记者不完全统计，如国家保密局局长夏勇，国务院法制办原副主任张穹，全国人大常委会副秘书长李连宁，最高人民检察院副检察长朱孝清，全国总工会副主席、书记处书记张鸣起，最高人民法院副院长江必新，司法部司法鉴定管理局局长霍宪丹等，均系西南政法学院78级毕业生中走上政坛的代表性人物。<br />
&nbsp; &nbsp; 恢复高考后，周强结束了两年的知青生涯，成为当时西南政法学院法律专业第一届学生；本科毕业后，再升入该校研究生院，师从金平教授攻读民法专业研究生。<br />
&nbsp; &nbsp; “当年423人进校，30年后，他们当中有14个副部级以上干部、100多个厅级干部、70多个律师、100多名大学教师。还有一些人在国外。”负责西南政法大学新闻宣传工作的张北坪老师在接受媒体采访时曾介绍道：“78级学生的成就实在太出色了，他们被称为‘西政78级神话’。”<br />
&nbsp; &nbsp; 从西南政法学院毕业后，周强被分配到司法部，其间还曾挂职担任深圳市司法局局长助理半年。<br />
&nbsp; &nbsp; 在司法部工作时，周强和导师金平常有书信往来。金平给周强写过一首诗，其中两句是“许国不畏身家累，除弊应如金石坚”，勉励他好好为官。<br />
&nbsp; &nbsp; 在金平看来，从西南政法大学走出如此众多的社会精英，是改革开放30年国家在法治建设上的长足进步成就了他们，光耀中国政界的“西政现象”也是我国改革开放30年法治建设成果的缩影。<br />
<br />
<br />
&nbsp; &nbsp; 任期最长的团中央第一书记<br />
&nbsp; &nbsp; 1995年团十三届四中全会上，周强与孙金龙一起，被增补为团中央书记处书记；两年后当选团中央书记处常务书记。1998年团十四大上，38岁的周强当选团中央书记处第一书记。中国第一位“60后”省部级官员的身份，让他更加引人注目。<br />
&nbsp; &nbsp; 五年后的团十五大上，周强得以连任，从而成为1978年以来任期最长的团中央书记处第一书记。在团中央任上，周强开始了对环境问题的长期关注。他倡议发起的“保护母亲河行动”，为黄河水资源和环境保护作出了巨大贡献，他因此被联合国环境规划署授予首届“地球卫士奖”。<br />
&nbsp; &nbsp; 首创规范行政程序办法<br />
&nbsp; &nbsp; 2006年，46岁的周强开始了他主政地方的历练：接替调任国家工商行政管理总局局长的周伯华，出任湖南省委副书记、代省长，后当选省长，成为全国最年轻的省长之一。<br />
&nbsp; &nbsp; 在湖南的4年中，有着法学专业背景的周强，第一个执政措施是把省内55个行政执法部门的权力界限公之于众，践行“法学家治省”，提出要让湖南这个中部大省实现“弯道超车”。在行政法学圈内众所周知的是，周强一直是《湖南省行政程序规定》和《湖南省规范行政裁量权办法》的主要推手。<br />
&nbsp; &nbsp; 据《中国新闻周刊》报道，2007年3月，全国两会召开，周强偶遇西南政法大学老同学、时任湖南省高级人民法院院长的江必新和著名行政法学家应松年。当时应松年正苦于他参与的《国家行政程序法》历经多次起草而流产，他问周强：“能不能考虑先在湖南搞起来？”<br />
&nbsp; &nbsp; 经过一年半的起草、论证、公示，《湖南省行政程序规定》在2008年国庆节问世，从而成为我国首部地方性行政程序规定；这和后来的《湖南省规范行政裁量权办法》一起，均为全国首创，建立了规范行政程序和行政裁量权方面的基本制度。<br />
&nbsp; &nbsp; 最新消息称，今年4月，周强主持召开了《湖南省政府服务规定》起草座谈会，标志着《湖南省政府服务规定》起草工作的全面、正式启动。据悉，通过省级立法来专门对服务型政府进行具体、明确制度规范的，湖南省在全国还是第一个，这也将是我国首部关于“服务型政府建设”的省级政府规章，并有望在年内出台。&nbsp;&nbsp;本报驻京记者　陈宝成<br />
