													<h2>80后夫妻拒当房奴拟租房10年  家长高血压发作欲断绝关系</h2>
						
						
																											<div id="postmessage_637669" class="t_msgfont">80后夫妻不愿当房奴签下10年租房协议<br />
<br />
2010年05月25日07:50<br />
<br />
<img src="http://img.hexun.com/2010-05-25/123791864.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
租房合同<br />
<br />
　　房子二室一厅，位于大渡口区钢花路，每月房租700元，10年就是84000元<br />
　　只租房不买房，在一对80后夫妻眼中，这是理想的住房状态。在市区寻寻觅觅1年多时间，他们终于找到一位肯出租房子10年的房东，并于前天签下 10年租房协议。<br />
　　他们的举动遭到双方父母强烈反对。昨日，4位老人专程从老家赶来，以断绝关系相威胁，要求他们就算赔偿也要毁约。<br />
<br />
　　这对小夫妻<br />
　　不愿买房只想租房<br />
<br />
　　这对80后夫妻，男的叫汤耀辉，在位于江北区观音桥的重庆宗远物流公司上班。女的叫王端睿，在大渡口区一个陶瓷批发公司上班。<br />
<br />
　　两人今年都是26岁，双方父母都住在老家垫江县。小两口说，结婚1年多来，他们一直租住在大渡口晋愉绿岛小区17栋3楼。两人月工资加起来有 6000多元。与绝大多数80后结婚要有新房的要求不同，他们在结婚时就决定只租房不买房。<br />
<br />
　　“不是头脑发热的一时想法。”王端睿告诉记者，结婚前，她和丈夫也曾想过买房。看了许多楼盘，仅首付就得花6万元以上，还不算装修、家具家电等费用。<br />
　　汤耀辉说，缴了首付，没有余钱再投资创业，此外，省吃俭用还贷款，生活质量也会下降很多，万一买的房子跌价了，心里难以接受。租房的好处就有很多，诸如：工资中拿出少部分就够房租了，负担轻，余钱可以用于改善生活质量；住得不顺心可以换房子租住；闲钱存银行吃利息，有机会还可以投资创业。想来想去，他们都觉得，租比买划算。于是，找一套二室一厅的“永久性”出租屋，成为他们的目标。<br />
<br />
　　找了一年多<br />
　　签下10年租房协议<br />
<br />
　　王端睿说，去年7月以来，她和丈夫跑了大大小小的二手房介，没找到一位愿意把房子长期租给他们的房东。<br />
<br />
　　大渡口区锦愉社区党支部书记王云霞说，本月初，王端睿请她帮忙找永久性的出租屋。当时，她就觉得难度很大。正当她为帮不上忙而烦恼的时候，上周五，一位在江苏省上班的朋友回来玩，她说起此事，对方称有一套想长期出租的房子。<br />
<br />
　　在江苏南京上班的36岁的苏存效说，他在位于北部新区的一家世界500强企业重庆公司当销售主管。去年5月，他的这套大渡口区钢花路的两室一厅房子装修好后，原本打算自己住，却被单位调到南京分公司销售部当经理，还被告知要待10年以上。他只好将妻子和孩子也接到南京，正愁新房如何出租，王端睿夫妻的要求，和他一拍即合。<br />
<br />
　　王端睿说，上周五晚上接到王云霞电话后，她和丈夫商量了几天，于前日正式和苏先生签下10年租房协议。<br />
　　记者昨日在王端睿家看到，这份协议写明房屋出租时间为2010年6月1日至2020年6月1日，租金每月700元。<br />
<br />
　　长辈不接受<br />
　　双方父母强烈抵制<br />
<br />
　　“岂有租房住10年的道理。”昨上午赶到市区来劝说的王端睿母亲李香勤说，前日，接到娃儿打来电话，声称签了10年租房协议，她当即被气哭了。<br />
　　家住垫江县新建路26号的汤耀辉父亲汤志强很气愤地说，前日与亲家见面后，大家一致认为，“成家立业”中的“家”，就是指房子。如果没有属于自己的房子，就没有基本的生存保障。双方家长讨论了3个多小时后决定，先以泪动情、以理服人的态度，劝说娃儿毁约，赔点违约金都可以；如果不能见效，他们就说把老家房子卖掉，准备在市区买套房子；再不行的话，他们就以和娃儿断绝关系相威胁。<br />
<br />
　　李香勤指责女儿很傻，哪有让丈夫给自己租房住。将来，孩子出生了，莫非也让孩子跟着父母一起居无定所？ 汤志强说，他甚至骂儿子没本事，只能让妻子睡出租屋，没想到娃儿很犟，随便家长怎么说，他们就是不愿买房。为此，娃儿他妈还被气得高血压发作。<br />
　　“年轻人晚买房都可以，但房子必须买。”汤志强说，哪怕当父母的砸锅卖铁也要帮他们买，一定要逼他们毁约。昨日，汤志强夫妇和亲家一起赶到市区，如果儿子和儿媳还不肯放弃10年出租协议，就威胁和他们断绝关系。<br />
