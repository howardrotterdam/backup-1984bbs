<br />
昨日，记者在东坑一处不足10平方米的出租屋见到了王秀勇。他说，他是山东菏泽人，3岁患小儿麻痹症，双手不幸失去活动能力，1996年来到东莞。平时他在镇区的文化广场或者天桥下用双脚进行写字、磨刀、切菜等表演，卖艺为生。由于多年走街串巷卖艺，因此对东莞多个镇街的地形十分熟悉。<br />
<br />
　　他说，东莞一直大力打击黄赌毒，因此他也想尽一份薄力，在最近的近一个月时间里，他一直在东坑、大朗等镇街暗访卖淫窝点，经过多种途径和长时间的蹲点核实，他确定了在东坑和大朗的多个发廊和出租屋是卖淫窝点，随后他将核实的信息手绘成一张图。“我已经交给警方了。”他说，希望社会上能多一点正气，人人都来参与打击黄赌毒。<br />
<br />
　　记者在他绘制的“窝点”图上看到，他用坐标、门牌号码、路名等详细地标注了哪里是卖淫窝点，有多少家等信息，熟悉当地地形的人看了后一目了然。<br />
<br />
