													<h2>画出扫黄地图的那位东莞老人说感到内疚</h2>
						
						
																											<div id="postmessage_670727" class="t_msgfont">“画扫黄地图，有点内疚”<br />
王秀勇仅住了一晚政府安置房就离开，后赴石排绘图未果<br />
类别:社会民生&nbsp; &nbsp;浏览量:2202&nbsp; &nbsp;版次:DA04&nbsp; &nbsp;版名:东莞读本 热闻&nbsp; &nbsp;稿源:南方都市报&nbsp;&nbsp;&nbsp; &nbsp; &nbsp; &nbsp;&nbsp;&nbsp;2010-06-10 <br />
作者:黎明 原创&nbsp; &nbsp;手机看新闻 全国订报<br />
摘要：上月底，双手残疾的王秀勇老人爆出了自己徒步数月绘出的“扫黄地图”，被画图镇区涉及东坑、大朗、石碣等五个镇街。<br />
(南方都市报&nbsp;&nbsp;<a href="http://www.nddaily.com" target="_blank">www.nddaily.com</a> 南都网)<br />
&nbsp; &nbsp; 王秀勇说，他现在每天呆在房里读杂文。<br />
(南方都市报&nbsp;&nbsp;<a href="http://www.nddaily.com" target="_blank">www.nddaily.com</a> 南都网)<br />
&nbsp; &nbsp; 南都漫画：邝飚<br />
&nbsp; &nbsp; ■ 热闻快读<br />
&nbsp; &nbsp; 上月底，双手残疾的王秀勇老人爆出了自己徒步数月绘出的“扫黄地图”，被画图镇区涉及东坑、大朗、石碣等五个镇街。正好赶上东莞扫黄禁赌的高潮，王秀勇风头一时无两，他手绘的“扫黄地图”，亦助警方获得不少扫黄成果。上周，在官方陆续表达了对他行为的“敬佩”之后，王秀勇老人却宣称不再绘制“扫黄地图”，这里面的乾坤耐人寻味。<br />
&nbsp; &nbsp; 热度系数：★★★★★<br />
&nbsp; &nbsp; 住政府提供的房子，会让人以为自己是以画“扫黄地图”来要挟政府给自己好处。<br />
&nbsp; &nbsp; 房子很舒服还有热水，但是我的内心却没法舒服。<br />
&nbsp; &nbsp; 毕竟我生活很不容易，她们也很不容易，所以我选择不继续画下去了。<br />
&nbsp; &nbsp; 接下来我会关注查访食品安全方面的黑点，因为我自己深受其害。<br />
&nbsp; &nbsp; ――― 王秀勇<br />
&nbsp; &nbsp; 宣告封笔　再赴石排<br />
&nbsp; &nbsp; 王秀勇“火”了之后说，他不打算再画“扫黄地图”，但是6月6日夜里，他又出发了。(南方都市报&nbsp;&nbsp;<a href="http://www.nddaily.com" target="_blank">www.nddaily.com</a> 南都网)<br />
&nbsp; &nbsp; “翟崇碧挺有种的，我6日和7日连续两个晚上都在石排走访，竟然没把石排镇‘扫黄地图’画出来”，王秀勇昨日上午说。之前，石排镇委书记翟崇碧曾在媒体发话，邀王秀勇去石排画“扫黄地图”，并称“我相信他画不出来，因为石排已经没有黑黄赌毒”。<br />
&nbsp; &nbsp; 王秀勇是在住进东坑镇政府为其租好的出租房后，才去的石排镇，他坦言自己去石排并非意味着自己接下来要继续画“扫黄地图”，而仅仅是为了回应翟崇碧的画图“邀请”。<br />
&nbsp; &nbsp; 身无分文　求助政府<br />
&nbsp; &nbsp; 王秀勇的石排绘图之行并非一帆风顺。<br />
&nbsp; &nbsp; 据王秀勇称，他在石排的两天将身上的钱财散尽，“吃住都要花个几十块钱，而且我不知道什么时候把裤兜里的一些钱给掏丢了”，直到7日早上清晨，饿着肚子身无分文的王秀勇厚着脸皮，走进了石排镇政府。<br />
&nbsp; &nbsp; 据他回忆，接待的工作人员很热情，轮着表扬他徒步画图的行为人让人钦佩，“但是我说我没钱回家了就没下文”。最后，镇政府社会事务办的工作人员拿了表格让王秀勇登记资料，但是王秀勇只有“残疾证”没有身份证明。一轮交涉下来，让王秀勇觉得受了“歧视”，于是拒绝镇政府的帮助强行离开。<br />
&nbsp; &nbsp; 王秀勇说，自己并非以“扫黄地图”要挟政府，“我身无分文走投无路向政府求助，并没有觉得自己出名了就要求这要求那”。最后，王秀勇拒绝了镇政府保安私下给他的50元钱，出门就打了110求助。石排镇公安分局知悉情况后，便租了辆出租车并派警员开车尾随将王秀勇送离开了石排镇。<br />
&nbsp; &nbsp; 搬离东坑　拒绝安置<br />
&nbsp; &nbsp; 对于王秀勇的石排绘图经历，石排镇政府社会事务办工作人员证实他当天确来求助，“但由于社会救助支出需要一套流程，所以王秀勇老人有些误解”。<br />
&nbsp; &nbsp; 其实早在6月5日，东坑镇政府就为王秀勇安排了出租房，并宣称保证他的人身安全。当天，王秀勇便对南都记者宣称：不再画“扫黄地图”。然而王秀勇只在安置房里睡了一晚。“安排我住的房间周围都是警察宿舍，我打电话或者出门都有人会来关心，我不习惯这样的生活，而且这样根本没法去石排画图”。<br />
&nbsp; &nbsp; 除了上面的原因，王秀勇还觉得，住政府提供的房子，会让人以为自己是以画“扫黄地图”来要挟政府给自己好处，“房子很舒服还有热水，但是我的内心却没法舒服”。<br />
&nbsp; &nbsp; 王秀勇称，当天住进政府安排的房子后，有位“片区管理员”拿一沓钞票找到他，希望他跟记者联系不要再报道，“我一辈子都没见过那么厚的钱，我拒绝了他的要求，但他还是给了我200元”。王秀勇说他用这200块钱出发去了石排，继续画“扫黄地图”。<br />
&nbsp; &nbsp; 如今，王秀勇已经自行安顿在80元一个月的出租房里，王秀勇说现在去书店或图书馆看书，经常会有人怀疑他是不是画“扫黄地图”的老人，他索性就成天呆在家里读杂文，还有就是想给刘志庚书记写封信，谈谈扫黄禁赌。<br />
&nbsp; &nbsp; 对话<br />
&nbsp; &nbsp; “当时绘图，只因她们太吵”<br />
&nbsp; &nbsp; 王秀勇：我并不恨“小姐”，我恨的是那些政府“保护伞”<br />
&nbsp; &nbsp; 南都：在画“扫黄地图”后，你曾经接到报复威胁都没有停止画图，但是为什么选择在官方给你安排出租房和送你手机保证你安全后决定不画图了呢？<br />
&nbsp; &nbsp; 王秀勇（以下简称“王”）：我看到了《南方都市报》上的一篇评论，说我没有考虑到“小姐”所面临的难处，说我就像是那个阿Q调戏小尼姑的心理。我不懂得阿Q调戏小尼姑是什么心理，但是后来我问人后理解了。<br />
&nbsp; &nbsp; 记得我暗访发廊时会拿着一个盘扮成乞丐，上门去问那些“小姐”给口饭吃，小姐们大多数都会掏钱帮我，反而那些外表光鲜的人不肯施舍。我想起自己“扫黄地图”给这些“小姐”们的打击，有点内疚。毕竟我生活很不容易，她们也很不容易，所以我选择不继续画下去了。<br />
&nbsp; &nbsp; “她们太吵，我没法休息”<br />
&nbsp; &nbsp; 南都：很多网友质疑，你画“扫黄地图”是不是出于什么不可告人的目的。<br />
&nbsp; &nbsp; 王：当初我压根没想那么多，也没大家想的那么复杂。因为我原来住在东坑的那栋出租房，楼下就是涉黄发廊，里面的“小姐”就住楼上，是我邻居。有时候，女房东还和“小姐”开我玩笑，说我这么大年纪了怎么不找点乐子，我就说我没有钱。到了晚上，“小姐”她们就在隔壁房间吵闹，弄得我没办法好好休息，所以我就生气想着举报她们涉黄，于是才有了第一张“扫黄地图”。<br />
&nbsp; &nbsp; 南都：但是后来事情的发展让人怀疑你的动机，你究竟是一种什么样的心态？<br />
&nbsp; &nbsp; 王：我穷惯苦惯了，没想过捞什么好处，只是做人乐观点。早在媒体报道之前，我早就将“扫黄地图”交给了东坑警方，警方后来给我电话说要奖励我，我当时一是不相信警方真的会奖励我，二来我也不想出风头，后来是一位媒体报料人听说我有画这个东西，才被他向媒体捅出来。<br />
&nbsp; &nbsp; 南都：为什么选择画“扫黄地图”，不画别的？<br />
&nbsp; &nbsp; 王：从小到大就受教育黄毒赌是坏事，毒和赌对于我而言比较难取证，所以我就对“黄”下手。其实我自己是个乞丐，人家“小姐”也是养家糊口，她们就这一个经济来源，我当时没考虑好，所以最后选择了收手。另外，我认识一些打工的人，他们长期没能过夫妻生活，人有时候总需要渠道发泄吧。<br />
&nbsp; &nbsp; 其实我并不恨“小姐”，我恨的只是那些政府“保护伞”。我以前到派出所去举报过老虎机，谁知道那警察反过来问我“举报老虎机又没人给你钱，这么做有什么好处呢？”我对这样的行为很反感，我们应该做的事情，在“保护伞”眼中却成了不该做的错事。<br />
&nbsp; &nbsp; “接下来查访食品安全黑点”<br />
&nbsp; &nbsp; 南都：接下来有什么打算呢？是否还想见到卢伟琪？<br />
&nbsp; &nbsp; 王：暂时没有想法。我去过残联，去过政府，我这种人想找份工作确实不容易，尤其现在成了个“麻烦人”，别人更不敢要我干活了。不过，接下来我会关注查访食品安全方面的黑点，因为我自己深受其害。报纸上说，有专门有去查访过期食品的人，我会先学习取证方法还有相关的法律法规。<br />
&nbsp; &nbsp; 至于卢伟琪局长的好意，我在报纸上看到了，谢谢他。不过我觉得现在就算我和他见面了，也没有太多的意义，我还是趁这段时间多看看书。<br />
&nbsp; &nbsp; 南都：你没上过学，但是你一直强调你很喜欢读书看报。<br />
&nbsp; &nbsp; 王：我以前住的地方旁边有一报亭，每天两三份报纸都卖不出去，但是我每天坚持看报，而且喜欢看到那些贪官污吏被抓的新闻。我没上过学，过去几十年我一路流浪一路自学，虽然我经常写白字，也没钱买书，但是我站在图书馆和书店里看完了不少书，比如《驻京办主任》和《市委书记》这类反腐倡廉的书我最爱看，还有《杂文月刊》我每期不吃饭都买，这些书是我们社会进步的力量。<br />
&nbsp; &nbsp; 画图秘诀<br />
&nbsp; &nbsp; 我每去到一个新地方画图，就首先找收垃圾的人，他们经常走街串巷，对情况很熟悉。<br />
&nbsp; &nbsp; 我常从拾荒者和收破烂人员的线索入手，再进一步查证核实。<br />
&nbsp; &nbsp; 确定了疑似涉黄地点后，我会谎称剪头发，然后进入发廊首先寻找营业执照。有营业执照什么的一般都比较正规，而且是不是小姐从外表也基本能判断出来。<br />
&nbsp; &nbsp; ――― 王秀勇说，自己的流浪者身份成全了他绘图的能力。<br />
&nbsp; &nbsp; 采写/摄影：南都记者 黎明</div>

							
							
							
							
