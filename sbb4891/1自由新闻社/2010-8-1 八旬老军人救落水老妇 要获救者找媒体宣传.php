<br />
&gt;&gt;救人者孙老伯：要求宣传为了教育旁观者<br />
<br />
救人的老伯姓孙，连云港灌云人，今年82岁，1944年参加新四军，1953年抗美援朝之后被分来杭州工作。老伯特别强调，他不是第一个跳进池中的人。“我是第二个跳进池中救人的。第一个跳进池中的也是一个老头，年纪比我还大，84岁了。”<br />
<br />
孙老伯告诉记者，他要求媒体报道此事，不为自己的名声。“让我寒心的是，当时在场那么多比我年龄小的人，他们都没去救，结果还是我们两个80多岁的老头跳进池里。唉――现在做好事的人越来越少了，看见人处于危难的时候肯伸出手搭救的人不多了，所以，有必要把这样的事情宣传一下，教育一下旁观的年轻人。我自己都80多岁的人了，要那个名声做什么用？”<br />
<br />
对于那些怀疑他救人是为财的人，孙老伯说：“我是退役军人，参加过抗日战争、解放战争、抗美援朝，现在每月七七八八加起来收入过万，你说我还需要什么财？她(指何大妈)说要登门谢我，肯定是要带点礼物送过来，说实话，我不需要。”“你们小同志，不了解我们，不知道我们这代人的想法。”孙老伯表示，理解一代人要把他同他所处的年代联系起来，“我经历的历史塑造了我的世界观，我的世界观指导我的行为。我只是想通过宣传我的事情感染更多的人。我没想到这一举动引来那么多的误解。”<br />
<br />
针对当今社会好事难做、好事不做、做了好事有后顾之忧的现象，孙老伯指出，在这种氛围下，做了好人好事就越需要多报道。<br />
<br />
&gt;&gt;被救大妈：要向组织汇报我想不通<br />
<br />
被救的何大妈表示，对于孙老伯所表达的弘扬见义勇为的思想，她是赞同的。“我们当初通电话的时候他也说过，是想为大家树立个榜样，好让大家都学习他的精神，提高社会风气。我觉得这个没什么错。”但是大妈对于老伯要求上电视，并让大妈向组织汇报救人一事的做法还是想不通。<br />
<br />
何大妈表示，最近她的身体一直不是太好。“等我腿脚方便了，肯定要去谢谢他的。”<br />
<br />
&gt;被救大妈的女儿：“雷锋做好事不留名”<br />
<br />
何大妈的女儿韩女士，是改革开放后接受新教育的第一代，有自己的想法，坚持自己的原则。谈起孙老伯求表扬的事，韩女士对此一直不满，“本来私下拎些礼物过去谢谢他挺好的，哪有这样来求表扬的？”<br />
<br />
在了解了孙老伯求表扬的用意之后，韩女士还是觉得“难以理解”。她认为孙老伯在记者面前说的和对她们母女在电话中说的不一样，比如，对记者讲他不是第一个跳进池中救人的，而对她们讲的时候说是第一个去救人的。虽然她承认了老伯的救人行为，表示感谢是应该的，但还是坚持“雷锋做好事不留名”的想法――“我就是不想让他曝光。”<br />
<br />
