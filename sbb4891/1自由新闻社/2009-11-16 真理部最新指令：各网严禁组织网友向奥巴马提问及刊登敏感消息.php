美国驻华大使<br />
<br />
洪博培(1960年3月26日－，英文名：Jon Meade Huntsman, Jr.)是美国犹他州的现任州长，共和党人士，于2004年及2008年当选。他是摩门教教徒，在加州帕罗奥图出生，曾在台湾传道，曾任美国驻新加坡大使，会说一口流利的汉语普通话。2009年5月16日，时任美国总统奥巴马宣布提名洪博培担任驻中华人民共和国大使。<a href="http://www.hudong.com/wiki/%E6%B4%AA%E5%8D%9A%E5%9F%B9" target="_blank">http://www.hudong.com/wiki/%E6%B4%AA%E5%8D%9A%E5%9F%B9</a><br />
<br />
是他向奥巴马提问的，不是两个党委学生。<br />
是前天在国务院网站上征集问题<br />
<br />
<br />
原文如下：<br />
[洪博培]第一，有这么多互联网使用者的国家，有6000万写博客的人，你知道防火墙的事情吗?第二，我们是不是应该自由的使用TWITTER?<br />
<br />
奥巴马：<br />
<br />
首先让我说，我从来没有使用过TWITTER。我注意到一些年轻人，他们一直很忙，有各种各样的电子器材，很笨重。但是我还是非常相信技术的作用，非常重视开放性。在信息流动方面，我认为越是能够自由的信息流通，社会就变得越强，因为这样子，世界各地的公民能让自己的政府负责，有一个问责制度，他们自己会思考，这样会有新的想法，鼓励创造性。所以我一直是坚定的支持互联网开放的使用，我是非常支持不审查内容，在美国我过去谈过，这是我们的一个传统，我也认识到不同的国家有不同的传统，但是我可以告诉各位，在美国，我们有没有受限制的使用互联网的机会，这是我们力量的来源，也应该受到鼓励的。<br />
<br />
但是我也应该很诚实的告诉各位，作为美国总统，有的时候我还是希望信息不是那么自由的流通，因为这样我就不需要听到人们在批评我，我认为很自然的。<br />
<br />
在人处于一个实力地位的时候就会想到，你为什么这样说我，你这样说是很不负责的。可是真实的情况是这样，因为在美国信息是自由的，因为在美国有很多人批评我说各种各样的事情，但我还是认为，这样才会使得我们的民主制度变得更强，使我变成一个更好的领导人，因为它迫使我听到一些我不愿意听到的意见，也迫使我审查我正在做的事情，每天都要审查，要看我是不是真的为美国人民做我能做的最好的事情。所以我认为互联网现在已经变成一个更强的工具，可以让公民来参与。<br />
<br />
实际上，我这次胜选，当了总统的一个原因之一我们能够动员很多年轻人，通过互联网来动员。刚开始的时候，没有人会想到我会赢，因为我们不是得到最富裕的支持者、政治上最有权利的人支持我们，可是人们通过互联网看到我们竞选，他们开始感到很兴奋，他们就组织起来成立一些竞选的活动、事件和集会，结果就产生了这些从下往上的一种行动，使我们很成功。<br />
<br />
这不仅在政府和政治，在企业界一样。像Google这种公司，不到20年前，它只是两个年龄跟你们差不多一样的人创业，本来是科学的实验，后来因为互联网，他们能够创造一个产业，这个产业使得全世界各地的商业发生一场革命。<br />
<br />
所以要不是有很自由的开放性，就像互联网所提供的开放性，那Google不会存在，所以我很支持一个做法，就是不要限制互联网的使用、接触或者像TWITTER这种信息技术，越开放越能够沟通，使全世界联系在世界。像我的两个女儿玛丽亚和娜塔莎，一个是11岁，一个是8岁，在她们的房间可以上网，通过互联网可以达到世界任何地方，可以学到她们想学的内容，这是她们巨大的力量，她们拥有这种力量，也有利于促进相互理解。<br />
<br />
就像我刚才所说的，技术也有负面，恐怖分子也可以通过互联网做一些以前他们做不到的事情，有一些极端分子也可以动员。当然开放性肯定要付出某种代价，这是不能否认的。可是我想好的远远多于坏的，所以还是保持开放是好的，这是我很高兴互联网也作为这个论坛的一部分。最后两个问题。<br />
