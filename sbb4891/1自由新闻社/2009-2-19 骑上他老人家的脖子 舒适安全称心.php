&nbsp; &nbsp; 来源：参与 作者：凌沧洲<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 这是网易20日转发的一条吸引眼球的新闻――<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 19日，有网友曝光了湖南长沙世界之窗的惊人一幕。一年轻女子骑上毛主席肩头照相，网友纷纷对此表示愤慨。・・・・ &quot;太可耻了，居然骑在毛主席塑像头上照相!&quot;今日，网友爆料，称2月14日在长沙世界之窗目睹了图中&quot;可耻&quot;的一幕。帖子发出之后，网友纷纷跟帖表示愤慨，有人甚至提出要&quot;人肉搜索&quot;出这个&quot;脑残&quot;。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; <a href="http://news.163.com/09/0219/16/52HF2GKM00011229.html" target="_blank">http://news.163.com/09/0219/16/52HF2GKM00011229.html</a><br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 更要看的是此一新闻后的跟贴评论，截止20日8：55，对此新闻共有网民评论共41561条 显示30166条 ，在显示的30166条中，大量批判、辱骂此女子的留言，看后使人不禁要问：<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 今夕何夕？文革乎？文革卷土重来乎？！<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 对这位年轻女子的&quot;傻比、杂种、妓女&quot;的辱骂声中，使人感到：这究竟是一个弥漫着怎样的暴戾情绪的中国？这些喝着狼奶长大的愤青愤老们究竟为什么对毛如此炽烈？这种毛原教旨主义甚嚣尘上的喊打喊杀究竟能不能反应真实的中国？<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 答案在报道的片面和倾向性中，答案更在那被屏蔽了的一万条评论中。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 中国大陆的媒体报道此事，仍然属于宣传洗脑术，一方面放大了少数毛派群众的观点，一方面淡化了大量非毛派观点。媒体对此的片面报道，更对毛原教旨主义推波助澜。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 比如此新闻的标题，现在是：《女子照相骑上毛主席塑像 网友怒斥&quot;可耻&quot;》<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 客观公正的标题应该是：《女子照相骑上毛塑像 网友争议》<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 前一个标题有两点疑问，第一，怒斥可耻的网友不能代表全部，因为也有人叫好，有人以为应该宽容，有人说：&quot;骑了人民的人终归要被人民骑&quot;；第二，&quot;毛主席塑像&quot;应为&quot;毛前主席塑像&quot;。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 而新闻的开篇说：&quot;太可耻了，居然骑在毛主席塑像头上照相!&quot;，也完全是主观片面之见，因为我们看到跟贴评论中也有网民称应该朝此雕塑吐唾沫。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 今日中国严酷的言论环境，这就是一个典型的个案与标本。少数毛派群众的极端观点可以在网络上通行无阻，而非毛派、批毛派的群众观点却大多无法通过审查，偶尔有漏网的被显示在网络上，却不得不以暗喻、谐音等方式，这确实让人联想起康雍乾&quot;盛世&quot;，清风不识字，何故也上网？！<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 同时，这些网站在把握言论尺度方面，看来也深得宁左勿右的奥秘，一些毛派群众的极端言论充斥着暴力与色情，如：&quot;搜出来。先奸后杀再奸再杀，卖到妓院。&quot;这样的言论不屏蔽，反而屏蔽了其他1万多条言论，岂非咄咄怪事？！<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 这种言论环境，这种宣传洗脑正进一步把国人推向愚昧偏狭的境地，还是有位网民有点幽默感：&quot;如此大胸美女骑玩，老毛一定会高兴的。&quot;<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 今日中国毛派群众与非毛派、批毛派的群众，应该搁置对毛的评价争议，致力于向前看，把主要关注点集中到民主、自由与社会公正上来，应该求同存异，看到双方对当今社会弊端的观察上有很多的共识，只不过开出的社会药方截然不同罢了。也可以呼吁在媒体上公开辩论。毛，无论是非毛派、批毛派眼中的暴君独夫也罢，无论是毛派眼中的伟人也罢，第一，遍地雕塑乃至花纳税人巨资去搞膜拜的社会肯定有问题；第二，毛在纪念堂的&quot;遗体&quot;，不管真的假的，都应该遵从中国风俗迁出，入土为安，这对死者本人也是一个尊重。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 否则，在这个星球上，人们还真以为中国人有&quot;干尸膜拜症&quot;，一如我12年前出版的《文化人批判》一书中描写的土著对他们的酋长干尸膜拜。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 我们不必从网络杀气腾腾、恶毒咒骂的言论中担心这位女子会成为中国的&quot;拉什迪&quot;，以中国人被征服近千年的性格，也都是的天桥把式，过过嘴瘾而已；但是从这起事件透出的中国言论自由的空间与现状，令人悲哀；这种网络言路控制，把许多文明理性的声音都屏蔽掉，反而听任暴力语言泛滥，不禁使人发问：网络管理者究竟要把中国人都变成什么样心胸狭隘、充斥仇恨的愚民呢？！<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 这位年轻女子，以其没有政治头脑的天真无邪，以跨姿骑上了毛的脖子，我们既不要把它看成批判暴政的壮举，也不会认同毛派极端分子斥为的亵渎之举。它，就是一个鲁莽贪玩女子的鲁莽贪玩之举，在嘻嘻哈哈的娱乐中完成了对道貌岸然的解构，起到了&quot;一骑胜过千支箭&quot;的高妙作用，想想文革，用毛头像的报纸擦屁股都会判死罪，想想1989，往毛像上扔鸡蛋被判了若干年徒刑；而今日此女骑上了毛的脖子，只是遭到恶毒谩骂而已，也说明毛原教旨主义虽然声嘶力竭，虽然回光返照，也终将日暮途穷，行将就木了。<br />
