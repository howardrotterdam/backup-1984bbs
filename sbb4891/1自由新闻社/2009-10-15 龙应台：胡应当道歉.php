													<h2>【RFI】默克尔：中国有义务对公民开放言论自由</h2>
						
						
																											<div id="postmessage_283857" class="t_msgfont">发表日期 13/10/2009 更新日期 13/10/2009&nbsp;&nbsp;23:15 TU<br />
<br />
德国总理默克尔和中国国家副主席习近平昨天晚上共同出席了第61届法兰克福书展开幕式。默克尔在开幕式致词中强调中国有义务对公民开放言论自由。她敦促中国必须在政治自由和言论自由方面表现出应有的责任感。<br />
<br />
中国是本届法兰克福书展的主宾国。这次中国派出阵容多达两千多人的庞大代表团前来参展。书展德国主办方坚持邀请中国流亡作家和异议人士参加，引起中国方面的不满。书展前举办的研讨会就一度发生中国代表团在中国流亡作家和异议人士上台发言时退席抗议的事件；而中方代表团的这种举动引起了德国媒体的极大反感。德国媒体激烈批评中国想把审查制度搬到法兰克福来强加给德国。<br />
<br />
在书展开幕前，德国总理默克尔就已在德国政府的网站上发表声明表示：言论自由对国家不是一种威胁，而是机会。她表示在会见中国领导人时，会明确地提出言论自由的重要性。在昨晚的书展开幕式上，默克尔又再次敦促中国开放言论自由。<br />
<br />
默克尔总理说：“ 最近一些年，中国的政治经济地位明显提升，但是在政治自由、言论自由方面，中国也要表示出它应有的责任感。”默克尔还以她过去在前东德共产主义制度下的生活经历，指出专制制度的书报审查对人类文化的危害。她回忆说，在那个专制的时代，她常常希望来访的亲友能够从西方偷偷给她带来一些书籍。默克尔说：“书籍可以创造一种不同的东西，对独裁制度是一种威胁，而民主是一种加强。”<br />
<br />
中国国家副主席习近平在昨晚开幕式上发言时，没有回应德国总理要求中国开放言论自由的呼声。习近平只说中国“主张一个和谐的世界，促进和平与繁荣”，希望“各方采取开放姿态，互相尊重”。<br />
<br />
法新社消息说，就在中国国家副主席习近平在法兰克福会展中心出席书展开幕式时，场外聚集了一些抗议人士，要求中国当局释放被关押的中国知识分子刘晓波和谭作人。<br />
<br />
与此同时，德国出版商协会主席洪非尔德在开幕记者会上表示，希望“中国的同业、作家和出版商能够享有他们所需要的自由，让他们能好好生活和工作。”<br />
<br />
本届书展主席布斯也强调说，书展必须“确保我们可以呈现不同的声音。”他期待“一个有争议而不总是合乎时宜的书展”。<br />
<br />
应邀参加书展的中国流亡诗人贝岭也在昨天的另一场记者会上表示，他和其他异议作家期待参观书展民众听到的不只是“官方作家”的声音，也应该听到中国流亡文学和异议作家的声音。他说：“我们有另一种声音，这是地下文学、地下诗歌的声音。”<br />
<br />
另外，在法兰克福书展开幕前夕，本台采访了总部设在巴黎的记者无疆界组织亚洲事务部负责人樊尚&#8729; 布鲁塞尔。记者无疆界组织的德国分部将参加书展的活动，尤其是参加10月15 日召开的有关中国出版问题的讨论会，这个讨论会将涉及所有有关中国出版业的议题。<br />
<br />
