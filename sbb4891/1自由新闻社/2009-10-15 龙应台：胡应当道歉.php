													<h2>龙应台：胡应当道歉</h2>
						
						
																											<div id="postmessage_282652" class="t_msgfont">法兰克福书展专访台湾作家龙应台<br />
<br />
10月13日晚，2009年度法兰克福国际书展正式开幕。今年的书展主宾国是中国。此前因一些中国异议作家出席法兰克福书展相关活动受阻，使得中国方面及书展主办方受到德语媒体普遍批评。默克尔总理在书展开幕式致词中强调了文学创作要保障言论自由和思想自由的重要性。中国国家副主席习近平则表示，国与国之间不应该用&quot;教诲&quot;的方式，而是对话和交流来进行交往和了解。刚出版《1949大江大海》的台湾著名作家龙应台在书展开幕式后接受了德国之声专访。<br />
<br />
德国之声：首先感谢您接受德国之声的采访，请您先谈一谈对刚刚代表发言的感受。<br />
<br />
龙应台：你看得出来，今天所有的德国讲者都是有备而来。有一个背景，是因为之前法兰克福书展已经出现言论自由的问题，还有有些作家被拒绝在外的整个事件。很明显，今天代表德国所发言的几位，在前面事件的阴影之下，一定要交代一下不可。刚刚出版协会的主席讲的非常好。他相当深刻地讲到言论自由对文化的发展，对国家整体国力的发展占怎样核心的地位。<br />
<br />
给我印象最深刻的是默克尔总理自己。她有一个非常特殊的身份。别的德国人还可以比较抽象地讲言论自由对于思想自由以及创作自由是多么重要，但默克尔总理本身是从东德共产党制度之下长大的人。整个的集权制度，以及当时如何为集权制度辩护，这些她都走过。她是以一个过来人的身份在讲，所以我觉得她的发言十分关键和重要。她也不喊高调，她只是讲到，记得以前在东德制度下生活是多么渴望西方的朋友寄书给她。完全是以一个过来人的身份在讲，我觉得特别动人。<br />
<br />
今天几位中方讲话的人中，我其实期待莫言可以在被允许的状况之下还能够讲出非常精辟的东西来。因为莫言的讲话是非常棒的，在朋友聚会时都很期待他讲话，而且他非常幽默，也非常的深入。今天我倒是觉得他没有表现出来他自己最精辟最深刻的那一面。铁凝，我是第一次听她讲话，我比较惊讶，非常的官方，不管是内容还是语言表现的方法。不管怎样，她是个作家，即使是作协主席，我都以为她会讲出内容更深刻的东西，所以今天铁凝的说话让我觉得有点惊讶。<br />
<br />
德国之声：法兰克福国际书展邀请中国作09年的主宾国。毕竟中国还是一个没有言论自由、出版自由的国家，中国作为主宾国，吸引这么多媒体的关注，吸引全世界的关注，那您觉得中方在之前有没有想到会遇到这样的压力?<br />
<br />
龙应台：我认为，中国过去这十年来，已经在国际和外交的事务上变得比较纯熟了，不是蒙着来的。我也相信他们知道今天的开幕大典上会有这样一个场面。前面地主国的每一个人都在谈思想自由的问题，所以当习近平出来之前，我心里其实在期待，他要怎样回应这个问题。他确实也回应了，可是他的回应基本上就是说，中国的国情是不同的，中国人权的概念是不同的，那这个是没有什么新鲜的东西。我只是觉得有一点点悲伤的是，中国很鲜明地有一个大国崛起的姿态，13亿的大国，但是在今天碰到这样的一个场面，书展其实就是一个思想、言论的自由到极致的展览，就是每一个国家在展览给你看，因为我们国家的创意自由到了什么程度，所以我们的创作就灿烂到什么程度。我今天心里比较难受的就是，这么大的一个国家，而且在文化上，中国是我的母国，你看得出，今天在中国出来讲话的那一面都非常的闷、非常的放不开来、非常的压抑。这么大的国家，这样的压抑，我觉得今天有点难受。就是说，你也可以面对面的碰撞，如果国情不同，因而对于言论自由都有不同的说法，那你说出来吧！你好好说吧！当前面的地主国的每一个人都那么重地讲这件事，然后你轻飘飘地飘过去，让人难受。<br />
<br />
德国之声：您刚才也提到，中国在谈到言论自由、新闻自由或者民主这种话题的时候，爱说自己有特别的国情或者是文化根源。从文化渊源而言，台湾与中国大陆有不少相似的地方，怎么解释台湾可以成功地施行民主制度呢？<br />
<br />
龙应台：台湾其实有点像默克尔自己从东德那个制度中长出来的，凡是走过集权体制的人其实对于自由有更深刻的体认。作为台湾人，我们也曾经在那样的体制中生活过。在我这个年代的台湾人长大的过程中，听到的永远都是说，国情不同。这完全是我们所熟悉的一种为自己辩护的说法。所以台湾应该是一个活生生的例子，就是说，不要用中国的传统文化作为借口，说中国的传统的土壤文化是这样的，因此自由的思想是不可能的。不是这样的。我不是说台湾的民主或自由是没有问题的，它问题多得不得了，跌跌撞撞而且犯很多错误。但跌跌撞撞，都是必要的过程。<br />
<br />
德国之声：我想谈一下您最近的作品《大江大海1949》。您还要求中国的国家主席胡锦涛在60年国庆庆典上道歉或表态。他没有道歉，这个大家都看到了，而且您的这本书在中国大陆被禁了。<br />
<br />
龙应台：当初那个报道其实是错的，我并不是说在中华人民共和国庆祝建政60周年的时候，胡锦涛应当道歉。我其实说的是，中国在庆祝60周年的同时，两边的领导人都可以利用这个时机，不只是庆功，也同时可以对“万骨枯”，就是对于60年前巨大的内战的灾难中，对那时所牺牲的人民有一个哀悼，主要是讲这个。两边包括马英九和胡锦涛，都可以利用那个机会对那些内战时牺牲的人说一声对不起。内战其实比前面的抗战可能还更残酷。<br />
<br />
我自己的书，其实并没有被禁，因为简体字版还没有出现，书要出现的时候才能被禁。只是在台湾和大陆版出现了后，在网络上禁止被报道和谈论，很多网页都被删除了。我也希望，这只是暂时的，是因为十月一日国庆的庆典。现在十一国庆已经过了，我希望《大江大海1949》这本书可以很快地出现。<br />
<br />
德国之声：刚才几位发言的人，包括默克尔总理也讲，书是一种文化的产物，但书也是商业的产物，尤其是电子书籍的出现。那么一个作家在表达自己的思想，在描述自己的感受的时候其实也要考虑到商业这方面，就是说，如果我的书如果写了卖不出去或者是不能被出版，那我作为职业作家的生计怎么办？您觉得，怎么能够在如今这种商业化的世界或气氛中还能够保持作家的独立性？<br />
<br />
龙应台：其实我倒是觉得商业和好的文学作品，不见得一定是矛盾的东西。我一点都不会瞧不起某些畅销的小说，因为你要写出真正畅销的小说还不容易。并不是说，好的文学就一定不畅销，畅销的文学就一定不好。另外，文学本来就是商业，文化本身也是产业。一个完整的社会，一个健康发展的社会里，我觉得各种作家都存在。有些作家知道怎样掌握市场。如果有本事写畅销小说，没有理由要求他不要为了市场而写，一定要为理想而写。如果写得好，为什么不可以为市场而写？另外有一些作家不太在乎市场，主要把文学当作，怎么样用他的文字和文字的艺术把他心里所想的、所追求的东西最精粹地表达出来。是这一类型的作家，那他就去追寻，就不太在乎市场。有时候会有一种结合，作家用他最精粹的语言表达他最深刻的思想以及最微妙的情感。当这个东西出来又畅销，当然是最好的。否则的话，我不觉得应该有一个最高的指导原则：商业是不好的，或者畅销一定是坏的，作家要维持理想的话就会不独立，不见得。<br />
<br />
德国之声：最新的年轻一代的所谓80后，甚至现在出现90后的作家。他们的作品中涉及政治的地方很少，当然跟他们的年龄可能有关。我有一个德国同事，他问了我一个问题，现在我想转问您：这能不能证明中国几十年来的这种新闻管制，没有新闻自由、出版自由的管制初见成效了呢？<br />
<br />
龙应台：这是有的。我看到的是一个钟摆，往另外一个方向，摆到另外一个极端去。也就是说，当国家对你非常压迫的时候，新闻管制非常严重的时候，你渴望自由。这个时候你所渴望的自由，是想尽办法去摆脱这个国家对你的控制。这个时候走到另外一个极端，其实就变成一种虚无。就是说，你对于谈社会责任或者谈使命感是一个羞耻的事情，或者是一个丢脸的事情、绝对不流行的事情。你会往那个方向走，会发展到一个程度，谈国家是最糟糕的事情，国家是最不值得一谈的东西，或者社会责任是最低级的东西，会往另外一个极端走。但其实这是一个钟摆的不是最健康的摆法。如果说这个社会里的比例就是不去思考社会责任，把使命感这种观念变成一个很脏的字眼，那么这个国家和这个社会是没有愿景的，是没有方向的。事实上，它会引向另外一种灾难跟极端。因为一个社会健康的发展，一定是有某一个比例的人，总是看得远一点，对社会总是觉得有责任的。一个社会不可能每一个人都有社会责任感，可是社会里要有一个方向，总有人决定这个社会的方向。如果这个社会里大多数的人不去考虑这个问题，这是一个非常危险的社会。<br />
<br />
德国之声：龙女士，您也是作为法兰克福国际书展邀请的嘉宾参加活动，您对今年由中国作为主宾国的国际书展有什么期望或展望？<br />
<br />
龙应台：我其实蛮高兴，中国作为今年法兰克福书展的主宾国。因为中国国家，这么大的一个社会里，本身不是一块铁板。里面有非常保守的力量，也有非常希望突破、而且有非常大的对于创意的能量的追求。同时存在各种矛盾的力量，彼此在一种博弈的状态里。像德国的书展把它当成主题国的时候，是给了中国整体社会更大的、跟国际进行第一手接触的机会。这种机会越多，视野和胸怀就会越宽广，那种狭窄的人的声音在社会里面相对会慢慢变少。对于中国能够参与法兰克福书展这样的活动，我是觉得参加得越多越好。而且国际社会除了给中国种种道德压力之外也可以给它很多的鼓励。</div>

							
							
							
							
