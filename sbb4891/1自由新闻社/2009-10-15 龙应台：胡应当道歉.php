													<h2>梁文道：假如它道歉？</h2>
						
						
																											<div id="postmessage_283156" class="t_msgfont">（《大江大海．一九四九》三之二） <br />
【苹果日报-牛棚读书记】<br />
<br />
革命是一场开端，所以暴力是必然的。正如汉娜．鄂兰（ Hannah Arendt）在《论革命》里所说的：「如圣经和典故所说的：该隐杀亚伯，罗慕路斯杀雷穆斯。暴力是开端，如果不运用暴力，如果没有忤逆之举，开端就无法缔造了」。从这个角度来看，龙应台的《大江大海．一九四九》就是那场开端暴力的拾遗了。 <br />
<br />
虽然多年前的《雪白血红》已经详细描述过四八年长春围城的惨烈，但是在龙应台的笔下重看一次那半年的经历，你仍然会抑止不住自己的情绪。 「围城开始时，长春市的市民人口说是有五十万，但是城里头有无数外地涌进来的难民乡亲，总人数也可能是八十到一百二十万。围城结束时，共军的统计说，剩下十七万人」。当时共军的策略就是严防死守，不许粮食和燃料进城，也不许百姓出城，他们的口号是「不给敌人一粒粮食一根草，把长春蒋匪困死在城里」。只不过被困死在城里的，主要是那上百万的市民。 <br />
<br />
他们吃掉了猫狗老鼠，吃掉了马，然后吃树皮、吃草、吃酒糟，最后吃人。一个幸存者记得自己在草地上看见一个正在移动的东西，「那是被丢弃的赤裸裸的婴儿，因为饥饿，婴儿的直肠从肛门拖拉在体外一大块；还没死，婴儿像虫一样在地上微弱地蠕动，已经不会哭了」。连围城的林彪都忍不住给毛泽东发电报说：「不让饥民出城，已经出来者要堵回去，这对饥民对部队战士，都是很费解的。饥民们对我会表不满，怨言特多说，『八路见死不救。』他们成群跪在我哨兵面前央求放行，有的将婴儿小孩丢了就跑，有的持绳在我岗哨前上吊」。 <br />
<br />
龙应台在长春考察，发现连很多当地人都不知道这件往事。 「在这场战役『伟大胜利』的叙述中，长春围城的惨烈死难，完全不被提及。『胜利』走进中国的历史教科书，代代传授，被称为『兵不血刃』的光荣解放」 。 <br />
<br />
被遗忘的死者当然要被唤回，被湮没的历史当然要被记起，所以龙应台才会花了一年多的功夫，几乎是呕心沥血地写出了这本书。但是循着智性的推理，我们仍然可以追问，她为什么要写这些东西？是想命名那些只剩下数字的死者？是想借着填补事件来重述一段不同的历史？ <br />
<br />
《大江大海．一九四九》出版之后，很快就被大陆当局当成了禁书，它还禁止媒体与网站发表任何评介文章。为什么？它怕什么？难道这本书动摇了六十年国史的基础，动摇了那套革命的叙述？我们知道这场革命的合法性建立在它推翻了「三座大山」，建立在共产党终结了国民政府的独裁统治。再反共的作家和学者也不得不承认当年的国民政府实在烂得可以，否则共产党就不可能在短短三年之间以摧枯拉朽之势终结掉国民党的政权了。难道龙应台正面驳斥了这一整个论述，从根本上否定了它的理据？ <br />
<br />
没有，龙应台的写作在这个意义上其实是「非政治」的，因为她甚至没去触碰那个很根本的问题：这到底是革命还是内战？汉娜．鄂兰(Hannah Arendt) 认为传统战争从不以自由为论据，只有到了现代，才有以自由正名的革命战争，这就是一般战争和革命的分别了。传统的战争只是「征服、扩张、维护既得利益，鉴于咄咄逼人的新权力崛起而拼命维持自己的权力」。只有革命宣称自己是「以自由对付暴政」，只有革命才会以自由的名义去正名那场开端的暴力。那么，龙应台是不是要用长春围城这样的例子去告诉大家那根本不是革命呢？不，她不讨论这个范畴的问题。 <br />
<br />
<strong>如果重写这段历史要有政治杀伤力的话，你要不就继续支持它是革命，要不就说它只是争权夺利的历史循环的又一幕罢了。说它是革命，说它牺牲了那么多的人命，然后你就可以质问现政权「你的革命承诺实现了吗？你所预许的自由在哪里？你对得起被牺牲的人吗？」这是有杀伤力的。说它只是夺权的内战，把它一切口号当成纯粹的矫辞，你则从根本否定掉了现政权的合法性；这也是有杀伤力的。但假如重写这段历史的最终预期就只是要现政权为死者道歉，要当局纪念国庆时不要那么兴高采烈呢？假如当局不那么小器，它大可以简单答覆：「好，我道歉。我什至可以为死者立碑，在长春建一座纪念馆」。然后呢？ </strong></div>

							
							
							
							
