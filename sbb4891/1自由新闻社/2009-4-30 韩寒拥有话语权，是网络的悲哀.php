 韩寒拥有话语权，是网络的悲哀【上】<br />
<br />
 <br />
<br />
韩寒是个没读过大学，在文化还不够普及的过去，被社会误认为当然也被他自己误以为是“少年写作天才”的一位误入歧途的青年。<br />
<br />
写韩寒我有一定压力，担心有肤浅无聊的小人会误以为我要借数落韩寒炒作自己……当然这亦或就是我杞人忧天以小人之心了……，这种下作的想法可能网友中压根儿就不存在！是啊，我也相信大多数朋友包括韩寒和他的粉丝，再看了我的文章后，也不会产生这样的误解的！<br />
<br />
我呢就姑妄言之网友们和韩寒就姑妄听之吧！<br />
<br />
最近韩寒的博客，言公务员是弱势群体！很多人较真，其实大可不必，和个孩子计较什么呢！<br />
<br />
韩寒引用的文章其实说的没错，现今公务员虽然很肥，但也有弱势的一面。如果把权力当成权利的贪官污吏，他就是弱势的，让个反贪局办事员就能给办了。如果公务员把权力真当成为人民服务的权力，他就是有力量的。而且这种力会变为势、威，即权势、权威，而且势力威不可挡！<br />
<br />
只是韩寒怎么会懂这些呢，文摘也是门很大的学问，你和个没上过学没工作自言也是个80后的不明事理的孩子，又能说什么呢！童言无欺嘛！ <br />
<br />
再者说，让个没文化的“毛孩子”拥有那么大的网络话语平台，然后又招惹的一帮“毛孩子”起哄架秧子，原本就是网络的悲哀！能怪孩子们的错嘛！<br />
<br />
包括现今很火的一些博客，大伙瞧瞧博主都是些什么人呀！像徐静蕾、郭敬明……都是些干嘛的！<br />
<br />
徐静蕾就是打着王朔的绯闻女友走出校门入圈的；郭敬明拿抄袭来的作品炫耀自己的精明和成功；至于韩寒，一堆不着四六的所谓作品，就狂妄片面的把大学骂成花钱买文凭的地方……<br />
<br />
这一切说明什么？戏子、骗子、无知、无耻……都进入网络审美系列啦，这难道不是网络的悲哀吗？<br />
<br />
说白了，点破韩寒这类人的把戏挤兑个孩子我都于心不忍，要不是网友的再三要求真就下不了这个口。<br />
<br />
这里我说的小可不是个年龄的概念，而是指见识。有志不在年高，有志也不在年少。韩寒的年龄也不能算个孩子了，生理上，一个十五六岁的男孩就有了性的需求，按法律十八岁就进入成年了。<br />
<br />
看他那身肌肉，和大姐大徐静蕾流畅过招，也都足以说明他已经发育成熟上下器官全长齐整了。<br />
<br />
再者说，韩寒也确实得有个人调教，不然孩子说些不着四六的话真出点儿事儿你再较真儿，他又以80后孩子无知为由，给你倚小卖小的撒娇，你也没辙呀！<br />
<br />
韩寒是纯种的商业化运作“概念炒作”托生的产物，所以容易给人以新鲜另类的错觉。韩寒这么个“写作天才”概念炒出来，就是为所谓的80后准备的！<br />
<br />
这里我说句题外的极端一点的话，谁心甘情愿的把自己当成80后或90后，谁就是傻子，谁就上了商人的当。因为所谓80后90后的概念，都是惟利是图的商人们人为的分类炒作。其实80后生的和90后生的消费喜好上能差哪儿去，商人们刻意分类的目的，就是挑起所谓这两代人之间的消费矛盾，然后分层重复消费，提高他们的利润。80后喜欢的，我90后偏不买，商人高兴啦，于是再给90后备一份。就是打对门的两个卖枣的，其实就是一家唱的双簧成心炒作的竞争事态，卖的都是同一种货色。<br />
<br />
那么炒作概念的目的是什么？是利用网络挣钱！经纪糊弄韩寒这样的没受教育不知深浅的年轻人，也比较容易驾驭。<br />
<br />
韩寒是个连中学都没正经读完的孩子，某种程度上讲，是当今社会没有话语权的弱势群体。他是靠自学，掌握了写作技能，也算个自学成才的有志青年。是商业化炒作，使他成了另类，迷失了自我。千佛山兴国禅寺门前写的：“暮鼓晨钟，惊醒世间名利客；经声佛号，唤回苦海梦迷人”，就是用来警示象韩寒这类迷失自我的朋友的。<br />
<br />
韩寒用自己的成长经历否定高等教育，是为迎合一部分的心理。似乎没文凭可以成才，他韩寒就是个例证！其实韩寒这一看问题极端片面的做法，正是教育缺失的结果。<br />
<br />
为何大学生没歧视韩寒，而韩寒反倒歧视否定大学教育呢？就是因为韩寒内心深处是不自信的！<br />
<br />
逆境出人才，顺境同样可以出人才。而且实践证明，顺境出的人才更多啊！这些道理韩寒不懂！<br />
<br />
成功模式并非你韩寒自学成才一种，也非你韩寒就是所有成功模式的终结者！这些道理韩寒也不懂！<br />
<br />
韩寒语录第一条就骂教育，可这是你韩寒压根儿就驾驭不了的一个话题。教育是要改革，可怎么改？你不懂！自由择业是三十年前人们渴望的，可如今真这么干啦，很多年轻人都成了“叶公好龙”者。觉得还不如父母那辈，毕业国家给包分配。<br />
<br />
如今中国教育严进宽出，真把它改成西方的宽进严出，是否适用于中国。你觉得宽进你韩寒可以进大学校门了，可寒春、寒秋、寒雪……就都可以进啦，那会儿知识的门槛就又会加高啦……<br />
<br />
自古以来，一朝天下闻靠得都是十年寒窗苦，便宜事没有啊！<br />
<br />
你韩寒能有今天，正是得益于中国教育的高难度。你的同龄的精英们都到高考战场上以试高低，不屑于和你争写小说这碗饭。所以你才有机会用写作证明自己，没有大学文凭的压力，也成了你写作的动力。象鲸鱼，因为没有鱼鳔，所以才拼命的运动肌肉，反倒因祸得福成为海中之王。你就是鹬蚌相争的受益者！<br />
<br />
条条大路通罗马，这是稍有见识的人就明白的理儿。你韩寒因没受高等教育，才看不到这一点，看问题才会失之偏颇。不受教育是弱点弱势，即使再掩饰也成不了优点优势。只是没人和你较真儿，读书人不会欺负弱势群体。你可不要一个人自我膨胀夜郎自大，真把自己当棵葱。临了没人拿你炝锅，可甭说事先没和你打好招呼！<br />
<br />
你就用你写的那几本书，否定高等教育的必要性，这正是你无知的表现。要知道，你写这点东西，要搁到一个受过汉语言文学教育的人身上，写的会比你更多、更好、更深刻。目前为止，你的经历价值，就是说明了一个自学成才者道路的艰辛。<br />
<br />
其实韩寒根本就不具备起码的问题探讨能力，他也没有交流和张扬的资本。尽管他一再标榜鄙视大学和文人，可至今他所能依仗的还是他那堆所谓作品。<br />
<br />
用韩寒自己的话说，他是一边当婊子卖淫挣钱，一边把骂婊子当做洁身自好立牌坊的资本。一语双关，一石两鸟！<br />
<br />
韩寒作品看半天也搞不懂他在说些什么，就是说韩寒的作品没有什么阅读的价值。它的价值就是印成铅字，摆在书店里，仅供当作炒作的资本和证明韩寒的成功的。<br />
<br />
策划人再借助于这个“成功”，玩儿概念多米诺的变化游戏：一堆文字变作品，很多作品是废品，有时废品变精品……这样经概念转换，就把韩寒演绎成了80后的部分人、没文凭的部分人、叛逆年轻部分人的所谓成功代言偶像，让他拥有了话语和被炒作的资本。<br />
