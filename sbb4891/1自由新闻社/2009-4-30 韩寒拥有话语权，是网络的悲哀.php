象韩寒、郭敬明这类人是没有资格作80后代言的，因为他们没有足以证明他们是精英的成果。而今我们很多人就是这样，不去看写作的内容，仅凭一堆书就做出判断。出版商正是抓住当今青年需要速成“偶像”成名“神话”的心态，印上一堆书，打造出了韩寒这类低素质低成本的成功模式，才使很多80后的人误把他当成自己这代人成功偶像代言，并趋之若鹜大加追捧。<br />
<br />
概念炒作就是迎合了一些人，只管结果不择手段盲目跟风的急功近利投机取巧心理。</div>

							
							
							
							
