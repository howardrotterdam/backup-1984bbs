韩寒拥有话语权，是网络的悲哀【下】<br />
<br />
 <br />
<br />
韩寒后续的做法也是效仿的戏子成名的路数，借助于80后代言人的身份，学谢霆锋搞姐弟恋，还整些性感照片张扬色相，至今也没拿出一篇有分量的作品，没有什么真正有助于80后成长的语录感言。作品中没有思想，张扬的也是一种很颓废的生活情绪。<br />
<br />
在博客上整天对国家政策说三道四，像个不着四六的孩子学说话，驾驭起来那么力不从心。<br />
