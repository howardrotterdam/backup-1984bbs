韩寒很狡猾，他的博客声明：不参加研讨会，交流会，笔会，不和格子PK，不和石康飙车，不和闻华舰交战，不签售，不讲座，不剪彩，不出席时尚聚会，不参加颁奖典礼，不参加演出，接受少量专访，原则上不接受当面采访，不写约稿，不写剧本，不演电视剧，不给别人写序……。<br />
<br />
很多人会误当成他的另类洒脱，其实他就是利用这种歪打正着的方式，躲过了自己知识欠缺和别人正面交锋力不从心的尴尬。<br />
<br />
你就在他声明的“不”字后面加个“会”字，那才是他心有余悸的后果。也就是：不会讲座、不会写约稿、不会写剧本、不会演电视剧……等，这就是狡猾的人的手段，先发制人，和象棋高手比跳棋……！<br />
<br />
不过，我确信韩寒的这个声明绝非是在作秀，我相信会有帮令人厌恶的傻瓜，上赶着为难个中学毕业的半拉孩子。那些所谓研讨会、交流会、签售、讲座、写剧本……等，也皆是充满了铜臭气的商业活动。所以韩寒才不得不以这种不礼貌的声明方式加以拒绝，这说明韩寒还具有可塑的良知。但韩寒也应反思，招惹来这么帮不着四六的家伙，是不是也有物以类聚之嫌呢！<br />
<br />
其实韩寒的内心深处也很自卑、很胆怯、很痛苦，他既无大学的受教育的学历，又无年龄时空的资历，怎么可能会比同龄人拥有更多的自信呢！人啊，除去受教育间接成长，就是到了一定年龄自然的“随心所欲”，除此没有别的渠道。<br />
<br />
在文化普及的今天，再要打造扮演韩寒似的无师自通的“天才”，不是无知也是自欺欺人啦。其实韩寒也很可怜，他是被人利用生生被搓到这么个很不适宜自己的高度。小小年纪，就被虚无起来总也消不了肿。所以才挤兑的他，东躲西藏的不敢见人。<br />
