韩寒的语录我就不摘啦，因为就是个孩子搞笑。仅由韩寒的语录的开场白，就可对韩寒无助和悲哀略见一斑：<br />
<br />
看开场“天下唯寒堵尊”这句，说明了他的无知者无畏；<br />
<br />
“韩寒经典语录”与其叫语录，不如叫说话，其实都是些老生常谈，拿来当语录说明了他的敝帚自珍；<br />
<br />
“看贴不顶者一辈子没钱赚”，说明这是他最在意的。<br />
<br />
韩寒最在意的是什么，那就是钱！据说回帖是他唯一能显现自我增加收入的方式，为了钱，所以他才会直白的赌咒。<br />
<br />
“有事寄信，上海市邮政信箱……”留下信箱，说明他还没洒脱到真不想见人，只是选择更适于他的更容易被他驾驭的见面方式。<br />
<br />
说这些，无非就是希望更多的青年不要效法韩寒，不要进入韩寒的误区。至于韩寒本人，当然也不能否认这是个挺努力挺刻苦的青年。自学之路是很艰辛的，韩寒骨子里很羡慕文化。他选择了写作，也是出于想和文化结缘。我给很多没受过高等教育的朋友出主意，让他们尝试写作，因为这是唯一不需要多高学历就能去尝试的文化活。<br />
