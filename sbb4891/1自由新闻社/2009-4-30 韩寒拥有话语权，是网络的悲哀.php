这里我得奉劝韩寒不能再虚着，虚着的结果会把人逼得走投无路，同样的话我也给刘翔说过。传媒就是想把人和现实脱节开来，制造神话提高发行量收视率。聪明人要学会摆脱及他们周旋，不要接神话的主人公这一角色，不然会被架空悬置起来活得很累。<br />
