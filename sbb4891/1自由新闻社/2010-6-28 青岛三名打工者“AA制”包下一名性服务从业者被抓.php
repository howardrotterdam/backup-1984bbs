6月20日上午，平度市公安局治安大队民警在浏览一家网站论坛时，两名网友先后发表的“平度依然存在卖淫嫖娼现象”的帖子引起了民警的注意。一名网友称，平度市灰埠镇的某浴池宾馆的老板娘李某，为躲避警方的查处，采取先联系嫖客再找来妇女进行卖淫的方式，与警方打起了游击战；另一网友则称，在平度市青岛路有一处出租房，一些神秘男女经常进进出出，他时常看到一名女子站在门前向过往男子招手、献媚，有卖淫嫌疑。民警当即将情况反映给平度市公安局治安大队王队长，在仔细阅读完帖子后，王队长立即安排民警在网友反映的两个可疑地点守候。<br />
