<br />
2010年04月01日09:23舜网-济南时报<br />
<br />
3月28日凌晨，山东济南清河小区一幢居民楼，一名13岁少女从5楼窗口跳下。虽经抢救，但仍未留住她年轻的生命。此前，她给父母留下了一封遗书，其要“捐献器官”的遗言令人动容。 <br />
<br />
花季少女深夜坠楼 <br />
<br />
今天上午，记者来到清河小区，目击者赵女士向记者讲述了事发经过。“28日凌晨3点10分，警察来到小区说有人跳楼，让我们出去看看是谁家的孩子。” <br />
<br />
随后，赵女士和女儿来到了楼下。“当时我们单元门口西侧停着一辆轿车，就在轿车车头的左侧，斜躺着一个女孩，女孩眼睛微睁，走近了才看清楚，躺着的这个女孩是隔壁楼上的欣欣(化名)，我女儿喊她，没有得到应声。”随后，120将孩子送往省交通医院抢救。 <br />
<br />
父母讲述伤心一幕<br />
<br />
今天上午，记者来到欣欣家。欣欣妈妈冯女士躺在床上，低头哭泣，由于伤心过度，她正打着吊瓶。欣欣爸爸李先生则凝视着女儿的照片，一脸愁容。 <br />
<br />
冯女士告诉记者，欣欣今年13岁，在济南稼轩初级中学读初二。平时寄宿在学校，半个月回家一次。上周五(3月26日)回了家。27日晚上，欣欣和奶奶睡在一间房中，并无异常。“当天凌晨3点多，我突然接到了邻居打来的电话，让我赶紧到交通医院，说孩子出事了。”这时，冯女士才发现家中孩子不见了。 <br />
<br />
随后，在山东省交通医院急诊室里，冯女士见了欣欣最后一面。“孩子身上插着许多仪器，睁着眼睛，怎么叫也不答应了……”说到这里，冯女士痛不欲生。 <br />
<br />
记者在欣欣家厨房看到东侧一扇窗户开着，窗台上还留有脚印。据家人介绍，欣欣就是从这里跳下的。 <br />
<br />
对于孩子莫名跳楼，家长不能接受。他们认为孩子大部分时间在学校，怀疑孩子在学校受到过什么刺激。 <br />
<br />
学校称出事前无异常表现 <br />
<br />
今天下午5点左右，校长李先生接受记者采访时称，对于欣欣自杀一事，他们很痛心，也感到很奇怪。事发后，学校校长、年级主任、任课教师都赶到医院看望欣欣，慰问家长，了解孩子出现意外的详细情况。学校向其家长表示，将积极配合处理善后事宜。 <br />
<br />
据李校长说，欣欣生前活泼开朗，学习成绩在中上游，在学校表现不错，是班里的纪律班长，出事之前没有任何自杀的迹象和异常表现。 <br />
<br />
目前，学校已经主动联系了警方，他们希望司法机关能介入处理此事。 <br />
<br />
女孩遗书留言要“捐献器官”<br />
<br />
28日早上，欣欣的家人在家中厨房发现了她留下的《遗书》。今天，记者在欣欣家人出示的遗书复印件中看到欣欣留言说：“请爸妈把我能捐的器官捐出去吧。虽然我是完整的出世，但是能让别人完整，也算是我的心愿吧。他们也希望更好的生活啊！Thanksalot(多谢)~” <br />
<br />
在这封遗书中，欣欣并未明确透露自杀的原因，但记者发现，她对死亡之后灵魂释放的一些说法，却显得与她的年龄极不相称。在遗书中，她嘱咐自己的父母和奶奶要注意身体，妈妈“也别太累了”，要让爸爸少喝酒，少抽烟，叮嘱妹妹要好好学习…… <br />
<br />
看着遗书中最后一句“我永远爱你们，死了也一样”，欣欣的父母在记者面前再次痛哭起来…… <br />
<br />
欣欣遗书内容摘录：<br />
<br />
“能让别人完整，也算是我的心愿”<br />
<br />
当困意来袭，闭了双眼。看到一个黑影和一个白影，人模样，向我招手。这个噩梦困扰了我好长时间了，偶尔觉得这种生的欲望越来越弱了，现在经常想起要释放，以为去了天堂就可以解脱一切。嗯，我想体验一下灵魂被释放的感觉。是好奇心占据了我心的百分之百。别难过，其实我也不想这样。但是我真的真的不想再在这个世界上生活，因为这个世界太丑陋了…… <br />
<br />
还有，请爸妈把我能捐的器官捐出去吧，虽然我是完整的出世，但是能让别人完整，也算是我的心愿吧。他们也希望更好的生活啊。Thanksalot~。 <br />
<br />
妈，谢了，这么多年一起陪我学习，教我这教我那。不过也别太累了，你看你，知道自己发烧感冒还去上班，身体垮了，可就什么都没有了。爸也一样，他胃不好，别整天让他喝那么多酒，少抽烟，对肺不好。 <br />
