奶奶，你老了，身体不好，又有心脏病，别有事没事儿着急上火的，犯了病身体气坏了，可是自己难受啊。 <br />
<br />
祺祺，你要好好学习啊，你在清河的这所学校真的不好……总之要把基础打牢，乖，以后要听话啊 <br />
<br />
给你们说，我真的不是有意伤害你们的，我知道你们会很难过，但是我有好多原因离开： <br />
<br />
一、我很好奇死后，灵魂的归宿。灵魂还能否感知一切呢？以及许多许多问题。 <br />
<br />
二、咱家不算富裕，每个人都那么辛苦挣钱，我过意不去，不想看你们两鬓变白，日渐劳累的样子。 <br />
