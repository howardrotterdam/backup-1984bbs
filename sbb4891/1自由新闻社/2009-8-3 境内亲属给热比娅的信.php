更可恨的是贺龙不仅自己不学习毛主席著作，而且还阻挠我们学，有一次他问我：“你在学什么？”我说：“在学习毛主席著作。”他不满地说：“你在宣传部工作，不能犯错误太多，特别是《论共产党员修养》，你这个小党员要好好看，研究研究，你就是政治嗅觉不灵，干不了什么大事，一定要向《论共产党员的修养》请教。” <br />
<br />
一九六六年我去大连养病前，贺龙又问我带些什么东西，我说：“我带了毛选四卷和主席语录，准备读一读。”贺即强调说：“你应带《论共产党员修养》，你这个小党员应好好学习刘主席著作，不学怎能行呢？”言下之意叫我不要学毛主席的著作。贺龙反对活学活用毛主席著作的野心不是昭然若揭了吗？ <br />
<br />
有一次贺龙对我说：“你的毛病就是吃不了亏，脾气不好，君子报仇十年不晚嘛！”这一语道破了贺龙篡党篡军的野心，贺龙就是一贯反对毛主席，一直等待时机，妄图变天。 <br />
<br />
一贯反对和敌视毛主席著作的三反分子贺龙，突然在一九六六年二月指示薛明与何家为（贺龙办公室副主任）起草了一个贺办学习毛主席著作的指示，里面是有鬼的！一是贺龙知道文化大革命将要在全国开展，贺龙作贼心虚，怕露出其一贯反对毛主席的马脚，急忙打起了这面红旗；二是贺龙对军委表扬林彪同志办毛主席著作很不服气，贺龙就不满地说过：“我就不相信他们能学得那么好？我们也要搞个样子！”以此反对林副主席，与林彪同志唱对台戏，更甚的是贺龙对贺办的学习指示说：“学习毛主席著作要反对教条主义。”把毛主席著作诬蔑为教条主义，其心何等险恶！贺龙就是打着学习毛主席著作的招牌来反对毛泽东思想。 <br />
<br />
三、广结党羽、阴谋政变：一九五九年贺龙到天津大比武，当时是彭真、罗瑞卿同去的。贺、彭二人同住在水上俱乐部，贺龙住楼上，彭住楼下。一次中饭时，罗、贺、彭三人大谈比武，罗得意地说：“我们军事上就是过得硬，就是突出政治。”声落，三人发出了使人肉麻的大笑。贺、彭、罗以为阴谋得逞，大功告成，天下即将到手，得意忘形，可惜这班混蛋错估了形势，高兴得太早了！饭后，彭真向罗瑞卿说：“你回北京了吗？”罗答：“我要留下给部队讲一讲话。” 说完，转向贺龙说：“你还有什么指示？”贺别有用心地说：“毛主席对大比武也很支持嘛！”贺龙就是罗瑞卿的后台，其竟敢冒天下之大不韪，造谣攻击中国人民心中最红最红的红太阳毛主席，其心多么恶毒！ <br />
<br />
罗瑞卿与贺龙的关系极为密切，罗常到贺龙家，有时罗把电影带来贺家放，一起看电影，谈笑风生，感情之深不难理解，罗对贺的讲话都是逐一纪录，驾乎毛主席和林副主席之上。 <br />
<br />
贺贼与吴晗来往也很密，贺常与吴一起打麻将、打桥牌、钓鱼，吹吹拍拍。吴晗写的书送给贺龙，并在书上恭恭整整地写上“敬爱的贺元帅、总理、同志，请指示。”贺对此当然非常欣赏。其意很明显，就是让吴晗这班黑文人通过文艺作品为其阴谋篡党篡军作舆论的准备。 <br />
<br />
文化革命期间贺龙对贺鹏飞（贺子）等人说过：“刘少奇的检讨是被迫的，毛主席错了！”这是明目张胆的拉开阵势的猖狂进攻，挑动贺鹏飞一伙，反对毛主席、反对党中央。贺鹏飞喜欢打篮球，贺龙就语气双关地对贺鹏飞说：“我是你们的领队，你妈是你们的政治指导员。”由此可见贺鹏飞这一伙在清华园干的坏事后台是谁不是清楚了吗？贺龙苦心孤诣地培养和支持贺鹏飞一伙，就是把自己的后代培养成像自己一样的资产阶级接班人。解放前我们的灾难还记忆犹新，贺龙妄图变天，我们一万个不准！ <br />
<br />
四、腐化堕落任意挥霍人民血汗：贺龙是一个大土匪，生活上充满了资产阶级腐化的臭味。平时只要贺龙起床电铃一响，贺家工作人员就为他一人忙碌，公务员赶忙进屋给他穿衣服、穿袜子、穿鞋；医务人员前来量血压；炊事员立即做美味的早餐，其每餐要保持一定热量，为此特让北京医院营养科的人来贺龙处精密计算食物所含热量。 <br />
<br />
贺龙每天至少有两、三次散步活动，常要老婆、秘书、护士、警卫人员陪同，因怕冬天院内散步冷，花了国家数千元，将原来的走廊披上了大玻璃，专供散步之用。夏天因大院太小，有时到体育馆去散步，由於那里人多吵闹，贺龙就将体育馆的人全部轰出来。 <br />
<br />
这个挥霍无度的贺龙还喜玩猴子，给猴子吃香蕉、桔子。一次从东北带回一只价值几千元的警犬，狗病了还请医生看、吃药、打青霉素，甚至还住医院，狗吃的饭比人还好，顿顿有肉有鱼，堂堂副总理、政治局委员，竟对猴子和狗如此关怀备至，这跟西方“自由”世界的大资本家整天无所事事，玩猫、玩狗没有什么区别。 <br />
<br />
贺龙这个土匪头子，干尽了坏事，对人民犯下了滔天罪行，像这样的一个腐烂透顶的修正主义分子，我们非把他打倒不可！毛主席教导我们：“敌人是不会自行消灭的，无论是中国的反动派或是美帝国主义在中国的侵略势力都不会自行退出历史舞台。”三反分子贺龙虽被揪出来，窃粜牟凰溃贺龙的走狗还通电话对我的母亲进行威胁、恐吓。我们要发扬痛打落水狗的精神，穷追猛打，坚决把贺龙斗倒！斗臭！<br />
