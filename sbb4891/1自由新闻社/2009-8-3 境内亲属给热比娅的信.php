																											<div id="postmessage_189195" class="t_msgfont">九年了。──她痛苦地回忆着。<br />
<br />
　　那时，她是强抑着对自己&quot;叛徒妈妈&quot;的愤恨，怀着极度矛盾的心里，没有毕业就报名上山下乡的。她怎么也想象不到，革命多年的妈妈，竟会是一个从敌人的狗洞里爬出来的戴愉式的人物。而戴愉，她看过《青春之歌》，──那是一副多么丑恶的嘴脸啊！<br />
<br />
　　她希望这也许是假的，听爸爸生前说，妈妈曾经在战场上冒着生命危险在炮火下抢救过伤员，这么可能在敌人的监狱里叛变自首呢？　<br />
<br />
　　自从妈妈定为叛徒以后，她开始失去了最要好的同学和朋友；家也搬进了一间暗黑的小屋；同时，因为妈妈，她的红卫兵也被撒了，而且受到了从未有过的歧视和冷遇。所以，她心里更恨她，恨她历史上的软弱和可耻。虽然，她也想到妈妈对她的深情。从她记事的时候起，妈妈和爸爸像爱掌上的明珠一样溺爱着她这个独生女。可是现在，这却像是一条难看的癞疮疤依附在她洁白的脸上，使她蒙受了莫大的耻辱。她必须按照心内心外的声音，批判自己小资产阶级的思想感情，彻底和她划清阶级界限。她需要立刻即离开她，越远越快越好。<br />
<br />
　　在离开上海的火车上，那时她还是一个十六岁的小姑娘，──瓜子型的脸，扎着两根短短的小辫。在所有上山下乡的同学中，她那带着浓烈的童年的稚气的脸蛋，与她那瘦小的杨柳般的身腰装配在一起，显得格外的年幼和脆弱。<br />
<br />
　　她独自坐在车厢的一角，目不转睛地望着窗外。没有一个同学跟她攀谈，她也没有跟一个同学讲话。直到列车钻进山洞时，她才扭头朝上望了一下行李架上自己的两件行李：帆布旅行袋，一捆铺盖卷，──这是她瞒着妈妈一点点收拾的。直到她和同学们上了火车，妈妈还蒙在鼓里呢。她想象着，妈妈现在大概已经回到了家里，也一定发现了那留在桌上的纸条：　<br />
<br />
　　我和你，也和这个家庭彻底决裂了，不用再找我。<br />
<br />
　　晓　　　华　　　一九六九年六月六日<br />
<br />
　　她想象着，妈妈也许会哭，或许很伤心。她不由又想起了从小妈妈对自己的爱抚。可是，谁叫她当叛徒的！她忽然又感到，不应该可怜她，即使是自己的母亲。</div>

							
							
							
							
