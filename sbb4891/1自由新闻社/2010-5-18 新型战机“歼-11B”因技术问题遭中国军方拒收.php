（2010-05-18　10:15am） <br />
<br />
（综合讯）共同社报道，据熟悉中国军事动向的专业杂志《汉和防务评论》（总部设在加拿大）最新的6月号称，中国军系国有飞机生产商生产的16架新型战斗机“歼-11B”因飞行时发生异常振动等原因，被军方拒绝收货。<br />
<br />
　　据称，“歼-11B”由中方以俄罗斯战斗机“苏27”技术为基础开发而成，中俄双方曾就知识产权问题有过纷争，此次飞行中产生的问题可能意味着这一技术转换面临失败。<br />
<br />
　　负责制造的是位于辽宁省的 “沈阳飞机”公司，2009年共生产16架。但交货前夕，空军飞行员在试飞该战斗机时飞机发生振动，因而军方拒绝收货。<br />
<br />
　　《汉和防务评论》称，之前四川省一家飞机制造商获得了“苏27”的许可生产“歼-11A”。3年前中国开始“自主开发”改良机型“歼 -11B”，但俄方则认为此举属于技术盗用。该杂志指出，去年10月中国60周年国庆阅兵时并未公开“歼-11B”也是由于上述技术问题之故。<br />
<br />
《联合早报网》<br />
