													<h2>胡锦涛在玉树题字黑板被收藏</h2>
						
						
																											<div id="postmessage_588555" class="t_msgfont">2010年4月14日7时49分，我国青海省玉树藏族自治州发生里氏7.1级地震，这是继2008年四川汶川特大地震后的又一自然灾害。4月18日，中共中央总书记、国家主席、中央军委主席胡锦涛在青海玉树地震灾区指导抗震救灾工作，并到玉树藏族自治州孤儿学校看望震后复课的学校师生时，在教室的黑板上题写了“新校园，会有的！新家园，会有的！”十二个大字，并带领着孩子们大声朗读。这朗朗之声传遍祖国大地，传递着中华民族坚强乐观、不屈不挠、永不言弃的精神。胡锦涛同志亲笔题字的黑板随即被玉树州孤儿学校校长像珍宝一样收藏了起来，现在该黑板已被送到青海省档案馆永久珍藏。<br />
<br />
胡总书记在黑板上的题词已成为抗震一线的珍贵档案和这场伟大斗争国家行为的重要历史见证。但是由于黑板粉笔字迹在黑板上附着力很弱，极易脱落，不能触摸、不能包裹、怕风等，属于很不耐久的字迹。如何使这一诞生于抗震救灾前线的特殊珍贵档案永久保存，是一切从事档案保护工作者的迫切愿望，是档案保护领域的新课题。<br />
<br />
<strong>4月22日，我校化学与材料科学学院博士生导师、陕西历史文化遗产保护教育部工程研究中心主任、全国著名档案保护专家李玉虎教授与王文军工程师一起赶往青海档案馆，现场对黑板的大小规格、基本材质、字迹状况等进行了实地考察研究。</strong>李教授说：“这块黑板与宁强县的黑板材质不同，宁强县的黑板是纯木质的，而这块黑板基体是铁制的。为了使这块黑板粉笔字迹档案永久保存，还需要做多次实验，在原来的保护技术上做些调整，并且要根据黑板的尺寸专门为其设计加工一个环保型防火、防虫、防霉、防有害气体的特藏装俱，为其营造环保型的安全环境。筛选加固剂最佳配方和制作特藏装俱大概需要一个月的时间，在这期间只要把黑板放好不动就不会有任何变化。”<br />
<br />
李教授与研究生一起曾对胡总书记2008年在陕西宁强县题字的黑板进行过技术处理，课题组的研究人员从黑板成份、材料筛选、实施工艺、原貌表征、高温高湿、氧化、有害气体、光等加速老化等方面进行了反复实验，多重筛选，做了大量研究工作，取得了满意的保护效果，此项目得到国家档案局领导的全力支持。经过研究人员一年多的努力，2009年12月该项目已经顺利通过国家档案局鉴定，鉴定会专家对此项目给予了高度评价。研究人员表示一定会再接再厉保护胡总书记为玉树灾区的题词，让这些珍贵档案永久保存，让世界人民知道中国是一个坚强不屈、团结奋斗的民族。<br />
