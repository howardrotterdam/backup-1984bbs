最后马英九失信了，他写信给我，“敖之大哥”，这封信我从来没有发表过。你可以看到马英九这里面谈到，他没有能够落实，他把这个事情延宕下来，他无可讳言的，他对我的两件事都没有实现，他难辞其咎，他向我道歉。马英九，弟英九，可是有什么用呢？最后他们还是撇开了我李敖，通过了预算。<br />
<br />
反帝篇《大舍与大爱》<br />
<br />
李敖：我在伪国会里面，最主要的一件事情，就是反对向美国买武器。美国毫无道理的，在跟中国建立邦交以后，又跟中国的一个省，发生军事采购的关系。它订了一个所谓的台湾关系法，是国内法，我请问中国跟美国建立邦交以后，可不可以订了一个夏威夷关系法，然后卖武器给夏威夷。你美国人一想就知道不对嘛，你怎么可以这样对待中国呢？可是台湾这个地区，由于蒋介石的原因，一直是一个在美国人面前，就亲美的、媚美的、低三下四的政府。<br />
<br />
所以这个政府，在蒋介石死了以后，一直流传到今天，对美国人就矮三截，低三下四。刚才我展示的给美国人，买这个铺路爪雷达，就是一个典型的例子。因此我在立法院，大力地反对向美国买武器，并且告了美国总统小布什，为什么告小布什？就是说既然你们说台湾关系法是你美国国内的法律，目的是保护美国的利益。那你应该送武器给台湾，如果台湾不买的话，你就不保护美国利益了吗？所以我们告了美国的小总统，小布什总统。<br />
<br />
我不但我告，我还联合了我在北京的我的女儿，优秀的李文博士，跟她一起告。李文博士，李文博士，李文博士，我们大特写看看李文博士，一起告。台湾帮我推动这个告诉案的，就是律师林宪同先生和律师杨宗儒先生，我们听听他们两位的讲话。<br />
<br />
林宪同（前台北律师公会理事长 大中联合法律事务所律师）：把美国国会的文件，还有台湾关系法里面有关军购的这个规定，从文字上面的解读，从外交的这个运作，到军购的必要性，他提出质询。这是非常精彩的，也可以说是李敖先生，在一任立法委员任内最精彩的精华之作。<br />
<br />
杨宗儒（行政院消费者保护委员义务律师 亘理法律事务所律师）：大师把我找去的时候，他告诉我说要告布什，告两百二十四个立委的时候，他第一句话一定问我你怕不怕。大师对于他的敌人，是这个打击上面，是绝对不遗余力的，但是如果你是他的朋友的时候，他对你那个私底下，不管是他的义气或是说他对你私底下的照顾，那个真的是没有话讲。这个是我对大师，最有印象的事情。<br />
<br />
李敖：为什么我这样反对美国？因为我是历史家，我清清楚楚地知道，美国怎么样地祸害中国，祸害台湾，当年的甲午战争，中国人把台湾割给日本，就是美国人从中搞的鬼。美国人是第一个用炮舰打台湾的人，第一个用飞机轰炸台湾的人，第一个在日本对中国侵略的时候，还卖钢铁给日本人制造武器的国家。<br />
<br />
看看这个画面，美国的参议员收到了中国人向他抗议的名单，这么长的名单抗议。就是说日本人在侵略我们中国，你们美国人为什么还卖钢铁给日本人？美国是个祸害中国的一个典型的元凶。很多人昧于这个历史，从蒋介石亲美政府以下，都做了错误的决定。所以当9.11事件发生的时候，美国人说怎么可以，宾拉登，你在9・11爆轰炸双子星的时候，你知道祸延多少无辜吗。<br />
<br />
今天很多所谓的正义之士骂起宾拉登来都说，你祸害了很多美国人，很多人是无辜，我给大家看看照片，当伊拉克战争发生的时候，一个小男孩子从美国的轰炸泥土的里面，轰炸的泥土堆里面挖出来浑身是泥死掉了，大家看，他身上还挂着一个奶嘴，请问这是不是祸延无辜。<br />
<br />
在美国打越南的时候，美国军人杀了越南平民的百姓，看到没有，老弱妇孺都被他杀掉了，请问这是不是祸延无辜。美国人有什么资格谈祸延无辜，美国是一个伪君子的国家，美国是个祸害中国的国家，美国是个祸害中国台湾的国家。<br />
<br />
我一再提出来，可是我必须说台湾是一个被美国宣传污染的地方，从上到下都对美国矮三截，这是我非常气氛的地方。美国人讲输出民主，请问你美国人的生活标准要不要输出，美国人输出人权，请问我们要不要同时接受你们美国人的生活标准。<br />
<br />
如果全世界人都接受美国的生活标准，要两个地球才禁得住我们消费，要八个地球才挡得住地球被污染，我们要不要这样做，美国是个可恶的国家，我最近正在写一本书叫做《阳痿美国》，美国得了病，我们要使它阳痿，这是我最近在写的一部书，很少人看到我对美国有这么清楚的认识，这是我的大功德。<br />
<br />
同时，对美国人态度以外，刚才杨宗儒律师特别告诉各位，我有很多官司是委托他帮我打的，杨宗儒律师是一个优秀的年轻律师，他替我服务做了很多这种细部的官司，比如说我告了两百二十四个立法委员，这些仗都是他帮我打的。<br />
<br />
我不但恨美国人，也恨日本人，所以当慰安妇事件，日本在二次世界大战强迫了中国台湾的女孩子们到他们军中做性奴隶的时候，联合国安全理事会责问日本人，你们要加入联合国安全理事会做常务理事国，要把你们屁股擦干净，日本人于是每个人出五十万来收买这些可怜的慰安妇，这些慰安妇当时在台湾还有四十五个。<br />
<br />
王清峰律师访问她们说，这个钱不能拿，为了国家的尊严，为了个人的立场，你们不能够接受日本人五十万。因为日本人羞辱你们，叫你们拿了五十万，签字同意当年自愿做军中的奴隶，老太太们，慰安妇们都不肯签字拿，都不肯签字拿，可是都很难过，为什么呢，因为五十万现金对她们构成一个吸引力，我就跟，透过李庆华跟王清峰律师说，我说我来捐一百件我的珍藏品来救她们，我们来卖这个钱来捐给慰安妇，结果我们卖了一百万美金。其中我的一些收藏品，包括胡适写给我的字，都由了不起的陈耀昌医师买走，一百万代价买走，一百万就可以救两个慰安妇，我拿出一百件艺术品做了一件义助慰安妇的活动，不要以为我完全在可怜慰安妇，我同时很强烈地恨日本人，仇恨是一个重大的力量，使我愿意拿出一百件艺术品来使日本人用钱来羞辱我们，来收买我们，这个毒辣的计划，不能够成功。<br />
<br />
我们真的做到了，我们打败了日本人，这就是慰安妇义卖的时候，这个现场，我跟我太太坐在这里，我和我的太太。我们再看报上登的大舍与大爱，收藏散尽天地，李敖沧海一笑。这就是我捐出一百件艺术品，我们卖到了一百万美金，能够解决了这个问题。</div>

							
							
							
							
