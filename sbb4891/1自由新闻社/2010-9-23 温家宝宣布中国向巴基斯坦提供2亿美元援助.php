<br />
中新社联合国9月22日电 (记者 张朔 孙宇挺)中国总理温家宝22日在纽约联合国总部出席联合国千年发展目标高级别会议。他提出了中国在此领域新的政策措施，并宣布中国将向巴基斯坦灾区再提供两亿美元无偿援助。【讲话全文】<br />
<br />
温家宝对联合国千年首脑会议召开10年来取得的成绩给予肯定。同时指出，千年发展目标在不同地区和领域的落实还不平衡，目标实现依然任重道远。中国将进一步加大扶贫开发力度，使千年发展目标在中国大地上如期实现。<br />
<br />
温家宝说，中国政府将进一步加强和改进援外工作，为全人类早日实现千年发展目标做出应有的贡献。他为此提出6项举措――<br />
<br />
第一，推动发展中国家民生事业发展。今后5年，中国将再为发展中国家建设200所学校；派遣3000名医疗专家，培养5000名医务人员，为100所医院提供医疗器械、药品等；援建200个清洁能源和环保项目。今后3年内，中国将向全球艾滋病、结核病和疟疾基金捐款1400万美元。<br />
<br />
第二，减免最不发达国家债务负担。截至2009年底，中国政府免除了50个重债穷国和最不发达国家的256亿元人民币债务。中国将进一步免除这些国家2010年到期未还的政府无息贷款。<br />
