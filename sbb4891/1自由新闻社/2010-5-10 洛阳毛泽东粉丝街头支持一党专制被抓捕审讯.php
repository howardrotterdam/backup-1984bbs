													<h2>2004年，郑州网上“毛派”被判刑</h2>
						
						
																											<div id="postmessage_608533" class="t_msgfont">河南省郑州市金水区人民法院刑事判决书<br />
(2004)金刑初字935号<br />
<br />
公诉机关郑州市金水区人民检察院。<br />
<br />
被告人张正耀，男，1948年7月15日出生，汉族，高中文化，无业，住郑州市金水顺河路40号院1号602室。因涉嫌犯危害国家安全罪于2002年7月9日被郑州市公安局中原分局刑事拘留，同年，8月15日转为在郑州市保安公司监视居住，2003年2月10日被解除监视居住。因涉嫌犯煽动颠覆国家政权罪于2004年9月9日被郑州市公安局金水分局刑事拘留，9月12日延长拘留期限至10月9日，10月15日被逮捕。现羁押于郑州市第三看守所。<br />
<br />
辩护人姚从勤，河南呐喊律师事务所律师。<br />
<br />
辩护人范德润，男，1955年6月1日出生，汉族，大专文化系河南呐喊律师事务所法律工作者，住河南省方城县城关镇东关村。系被告人张正耀之朋友。<br />
<br />
被告人张汝泉，笔名张纤夫，男，1935年7月28日出生，汉族，初中文化，系郑州百文依力股份有限公司退休职工，住郑州市管城回族区245号3号楼2单元13号，因涉嫌犯巅覆国家政权罪于2003年2月20日被中原公安分局国保大队取保候审，同年11月10日解除取保候审。因涉嫌犯煽动颠覆国家政权罪于2004年10月15日被郑州市公安局监视居住。<br />
<br />
辩护人李小玲、段军创，河南译达律师事务所律师。<br />
<br />
郑州市金水区人民检察院以郑金检刑诉(2004)1116号起诉书指控被告人张正耀、张汝泉犯诽谤罪，于2004年12月8日本院提起公诉。本院受理后，依法组成合议庭，不公开开庭进行了审理。郑州市金水区人民检察院指派检察员杨京鸽出庭支持公诉．被告人张正耀及其辩护人姚从勤、范德润，被告人张汝泉及其辩护人李小玲、段军创到庭参加诉讼。本案现已审理终结。<br />
<br />
郑州市金水区人民检察院指控，<br />
1、2004年9月初，被告人张正耀与其妻葛黎英(另案处理)商量找被告人张汝泉撰写文章，以便在9月9日借纪念XXX逝世28周年之际进行散发；后被告人张正耀鼓动张汝泉撰写文章，向其承诺如出事不用其负责；9月6日、被告人张汝泉将撰写的《XXX--我们永远的领袖》，署名“宋梅”的煽动性传单交给被告人张正耀，该传单恶意诽谤党和国家原领导人XXX、XXX；9月7日早，被告人张正耀将该文章复印件和100元钱交给王占清，让其印刷2000份；上午10时许，葛黎英将该文章以“影子”的网名上网后又下载，制造从网上下载的假相。下午4时许，被告人张正耀和王占清到郑州市京广北路38号楼对该煽动性文章进行讨论，并预谋商议9月9日散发传单的行动计划；9月8日，王占清将印制好的大部分传单交给被告人张正耀：9月9日，被告人张正耀以借XXX逝世28周年之名散发煽动性传单“XXX--我们永远的领袖”300余份，被当场抓获。<br />
2、2002年7月上旬，被告人张汝泉将一篇题目为《党的十六大和中国马克思主义者的战斗任务――关于召开党的十六大几个重大问题的严正声明》的煽动性文章交给被告人张正耀看，并称“这篇文章写的好、是我见过的最尖锐的”。被告人张正耀和其妻葛黎英看过后预谋给更多的人看。7月8日，葛黎英按约定到郑州市中原通讯印刷厂准备取该文章的印刷件时被抓获。<br />
<br />
针对上述事实，公诉机关提供了被告人陈述，证人证言，提取证明，抓获经过等证据，认定被告人张正耀、张汝泉之行为触犯了《中华人民共和国刑法》第二百四十六条第一、二款之规定构成诽谤罪。应当以诽谤罪追究刑事责任。<br />
<br />
被告人张正耀辩称，自己行为是合法的，只是向悼念XXX的人员散发，没有向广大群众散发，文章的观点来自XXX理论，不是捏造的。其辩护人提出的辩护意见是：张正耀的行为不构成诽谤罪。1、被告人张正耀在主观上并没有诽谤他人的故意。2、被告人张正耀在客观上没有捏造虚构的事实诽谤他人。该文章在内容上涉及XXX、XXX执政方针、路线问题，但仅仅是一个普通公民在世界观、社会观的不同看法而已。3、对于公诉机关指控被告人张正耀借纪念XXX逝世28周年之机散发传单300余份事实不清，各证据之问相互矛盾。4、即使被告人张正耀的行为构成诽谤罪，诽谤罪是告诉才处理案件，适用公诉程序不当。<br />
<br />
被告人张汝泉辩称，自己的行为不构成诽谤罪。文章中的所有观点都来自XXX无产阶级专政下继续革命的理论，没有故意捏造任何事实去诽谤XXX、XXX。写文章的目的是为了纪念XXX逝世28周年。其辩护人提出的辩护意见是，1、本案属于自诉案件，公诉机关程序违法。2、被告人张汝泉的行为不符合诽谤罪的构成要件。文章只是一种思想观点，不是捏造事实，不构成犯罪。3、被告入张汝泉没有散发行为，主观上没有诽谤的故意。4、被告人张汝泉没有散发行为，也不知张正耀散发。5、已死亡之人和团体不能构成诽谤罪的客体犯罪对象。<br />
<br />
经审理查明，2002年7月上旬，山西省农工委干部周秀宝给被告人张汝泉寄来一封信，信后附有一篇题为《党的十六大和中国马克思主义者的战斗任务一一关于召开党的十六大几个重要问题的严正声明》的煽动性文章。该文恶意诽谤原国家领导人XXX。文章中写到“XXX总书记在‘七一’讲话中，举起反革命修正主义叛党黑旗，XXX是死不悔改的机会主义的总书记，是要依靠手中的军权和枪杆子，来维持和确保自己作为党中央及全党的领导核心地位不变，继续实行XXX那样的垂帘听政，在全党全国人民面前暴露他机会主义叛徒的丑恶嘴脸，绝不允许在中国共产党和社会主义的历史上再出现第二‘西太后’”等内容。<br />
<br />
被告人张汝泉将该文章交给被告人张正耀，被告人张正耀和其妻葛黎英(另案处理)看过后，即预谋给更多的人看。2002年7月7日，葛黎英携该文章到“中原通讯印刷厂”联系印刷。后以1000元的价格商定印制6000份，次日取货。因该印刷厂及时举报至中原公安分局三官庙派出所，2002年7月8日上午，葛黎英如约到该厂取货时被当场抓获。被告人张正耀为此于2002年7月9日被刑事拘留．，同年8月1 5日转监视居住，2003年2月1 O日解除监视居住。被告人张汝泉为此于2003年2月2 0日被取保候审，同年11月10日解除取保候审。<br />
<br />
2004年9月初，被告人张正耀与其妻葛黎英两人预谋从互联网上下载文章，准备在9月9日借纪念XXX逝世28周年之际进行散发。因没有找到需要的文章，两人便商议找被告人张汝泉撰写文章。被告人张正耀找到并将其想法告诉了被告人张汝泉，二人商量了该文章的主要内容。2004年9月6日晚6时许，被告人张汝泉打电话通知被告入张正耀文章已写出，即《XXX--我们永远的领袖》，署名“宋梅”，后将该文c盘送至被告人张正耀家，葛黎英随即于9月7日上午1 0时54分将该文以“影子”，的网名张贴在“XXX旗帜网站”的“旗帜论坛”上。11时01分又将该文章下载存入c盘，制造“从网上下载”的假象。2004年9月7日晨，被告人张正耀将该文复印件和l00元钱交给王占清(另案处理)，让王占清联系印刷2000份；当日下午4时许，被告人张正耀召集王占清、王景春等人到郑州市京广北路38号楼对该文进行讨论、修改，并商议9月9日散发该文的行动计划。2004年9月8日下午，王占清从郑州市中原区恒利印刷厂取回印制成传单形式的文章《XXX--我们永远的领袖》2000份，将其中的大部分交给被告人张正耀。次日上午8时许，在郑州市金水区原河南省博物馆XXX塑像前，被告人张正耀借纪念XXX逝世28周年之名，将该传单在人群中大量散发，造成大批群众围聚，后被当场抓获。经查《XXX--我们永远的领袖》一文中，具有捏造“XXX篡夺国家政权，对XXX及其事业疯狂发泄刻骨仇恨，对XXX极尽攻击、中伤和污蔑之能，继续干着当年XXX所干的勾当以及“邓江之流”，代表帝国主义、资产阶级腐朽势力的利益，‘小丑’”等内容和措辞。<br />
<br />
上述事实，有公诉机关提供的下列证据予以证明：<br />
<br />
