14、张正耀在XXX像前散发传单的照片在卷佐证。<br />
15、张正耀供述：2002年的一天，葛黎英去华山路印周秀宝的文章《党的十六大和中国马克思主义者的战斗任务一一关于召开党的十六大几个重大问题的严正声明》，我看了文章，还可以，葛黎英去印文章我是知道的；2004年9月份，XXX纪念日快到了想写点东西。9月5日，我找了张汝泉让他写点文章，歌颂XXX时代光辉成就，联系实际情况，反映我们工人阶级的悲惨现状、反腐败、反复辟等内容，并告知了张汝泉此文章将在2004年9月9日在XXX像前散发。2004年9月6日，张纤夫把文章写好。9月7日上午，我约王占清在省中医院见面，给他一百元，让他印2000份。9月8日，王占清将传单交给我。9月9日我们到原博物馆XXX像前悼念XXX散发传单时被抓获。传单批判了XXX、XXX20多年执行修正主义路线给我们的党、给我们的人民、国家、广大劳动群众造成了灾难。<br />
