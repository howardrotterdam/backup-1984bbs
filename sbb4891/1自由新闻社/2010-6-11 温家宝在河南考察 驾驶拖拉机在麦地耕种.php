													<h2>温家宝在河南考察 驾驶拖拉机在麦地耕种</h2>
						
						
																											<div id="postmessage_674746" class="t_msgfont"><img src="https://yopic.us/images/oqo.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
温家宝同学在郑州又开拖拉机了，这先生好像喜欢开拖拉机，不是第一次。<br />
<br />
温+宝又登上一台“东方红”牌拖拉机的驾驶室，在司机的指导下挂挡、加油，紧握方向盘，稳稳地驾驶着，在刚刚收割的麦地里深耕、施肥，种下秋玉米。<br />
<br />
+宝幽默地向农民们问道：我的驾驶技术还合格吗？农民们回答道，很好！很好。(news.cn)<br />
<br />
2010-06-11 14:55:00　来源: 新华网(广州)　跟贴 0 条 手机看新闻 当前，我国小麦主产区由南向北正陆续进入“三夏”生产的关键时期。9日至10日，中共中央政治局常委、国务院总理温家宝来到河南省郑州、许昌等地，深入麦田、农户和粮库，实地察看小麦收割和收购情况。他还主持召开农民和基层干部座谈会，就农业农村工作进行调研。<br />
<br />
温家宝强调，“三农”问题始终事关全局。稳定粮食和农业生产，保持农产品价格基本稳定，对于管理好通胀预期，实现今年宏观调控的目标至关重要。我们要从全局和战略高度，深刻认识抓好今年粮食和农业生产的特殊重要意义，毫不松懈地抓好今年的“三农”工作。<br />
<br />
河南是我国重要的产粮大省，粮食总产量占全国十分之一、小麦产量占四分之一，均居全国首位。今年以来，河南克服低温等不利气候条件，狠抓春季麦田管理，夏粮有望继续丰收。<br />
<br />
9日下午，温家宝一下飞机，随即赶往许昌市。沿途所见，麦浪滚滚，满目金黄，一派丰收在望的景象。温家宝走进长葛市石象乡坡李王村的麦田里，掐下一个麦穗放在手里揉去外壳，看着饱满的麦粒，脸上露出高兴的笑容。正在麦地里忙活的村民们围在温总理的身旁，对他说：“一亩地能产千把斤小麦。今年气候反常，没想到会有这样的好收成。”<br />
<br />
“温总理今年3月在全国人代会上宣布，小麦最低收购价每斤提高三分钱，这让种粮农民吃了定心丸。”坡李王村支部书记李福成说：“今年开春气温回升晚，小麦返青时间又普遍推迟10多天。国家及时出台了对冬小麦弱苗施肥补助等七项措施，还追加喷农药，防病虫害、防干热风、防倒伏的资金，乡亲们的干劲更足了，麦情一天比一天好。”总理接过话头说：“国家针对今年气候异常状况临时追加了‘一喷三防’投入5.3亿元。现在看来，这笔钱花得及时、花得值得。”<br />
<br />
已经收割的几垄小麦地里，长出了几寸高嫩绿的玉米苗。村民们告诉总理，这是他们前两天趁着降雨间隙钻在麦田里点种的。温家宝蹲下身子认真察看。李福成随口说出了自己总结的四句话：喜雨催麦老，穗齐籽粒饱；喜雨促秋管，秋季能丰产。温家宝诙谐地夸赞道：你做的“五言诗”很押韵，一个“催”，一个“促”，两个字抓住了关键。麦田里响起一片笑声。随后，温家宝走进村子看望村民，并与他们座谈。温家宝对大家说，夏粮产量占全年粮食产量的四分之一，夏粮丰收就为全年粮食丰收奠定良好基础。粮食丰收，农业稳定，经济发展就有了坚实的基础。我们时刻都不能放松粮食生产。村民们响亮地回答：请总理放心，我们会下更大的力气把粮食生产搞好。<br />
<br />
作者：赵承当前，大批跨区的收割机正在从南向北转战各个麦区。为方便跨区机收，长葛市在过境的107国道旁设立了农机服务站。10日上午，温家宝来到服务站办公服务区。几张一字排开长条桌后面，公安、农机、石化等部门工作人员坚守岗位，提供交通、维修、信息等跨区机收服务。服务站路旁停着十几台联合收割机，分别从河北石家庄、湖北襄樊、河南驻马店等地开来。农机手们有的夫妻相伴，有的兄弟同行。温家宝关心地向他们询问农机补贴、跨区机收以及农机手的生活安排情况。总理对大家说：“最近柴油价格降低了，对跨区机收有利。春争一日，夏争一时，你们要快收抢收。”头发花白的老耿来自湖北，正在驾驶室里忙活着，满手油污。当温家宝向他伸出手时，老耿连连摆手，忙不迭地躲闪着：“我手脏，不握了。”温家宝举手坚持着，老耿感动了，他的手紧紧握住了总理的手。当两手相握时，温总理笑着对他说道：祝你一路安全。憨厚的老耿激动得不知道说什么好，连声道谢。<br />
<br />
离开服务站，行车不到十分钟路程，温家宝来到许昌新兴国家粮食储备库。储备库门前，热闹非凡。灵井乡刘庄村的吴新合和村民们一道用拖拉机拉来了今年新收的3000多斤麦子，等着装仓入库。温家宝向老吴了解了小麦的产量、价格，以及收购情况等。老吴指着拖拉机上的一袋袋新麦说：“已经验完了，二级麦，每斤能卖到9角2分呢。”他还用食指在编制袋上捅了个小洞，将流出的小麦捧到总理手里。温总理看完又小心翼翼地交还老吴，生怕撒掉一粒麦子。他笑着说：“这叫颗粒归仓。”温家宝还走进粮库大院详细了解小麦检验和收购过程。储备库负责人介绍说，今年粮库预计收购夏粮2.5万吨，所需要的仓容已全部腾出，5000万元收购资金全部到位。万事俱备，只等新粮入仓。温家宝沿着一个6米多高、狭窄而陡峭的铁梯，攀登而上实地察看了一个满藏小麦的粮仓。在收储新麦的6号仓库，他考察了卷扬机向空仓里输送新麦的情况。温家宝对粮库负责人说，要把农民的粮食收好，保障粮食安全，让农民满意、让政府放心。<br />
<br />
临近中午，天气放晴，艳阳高照。许昌县陈曹乡后孙汪村高产小麦示范区里，无边的麦田被延伸其中的条条道路劈开，像一块块金色的地毯，路边成行的白杨环绕仿佛为麦田镶上绿色的边。大型收割机正在麦田里往来穿梭，一派繁忙收获的景色。清风徐来，麦浪送香，温家宝来到示范区的地头，向农民们了解今年的收成后感慨道，天道酬勤，今年小麦丰收不容易啊。温家宝忍不住攀上3米多高的联合收割机驾驶室，与来自河北石家庄的农机手吕素彬一道，在这个充满希望的田野上，往来收割金黄的麦子，体验丰收的快乐和喜悦。在轰鸣声中，联合收割机所过之处留下一道道麦茬，麦秆粉碎还田，麦子颗粒归仓。一趟下来收割机满载而归，从机仓中源源不断地吐出饱满金黄的麦粒。农民们随即将麦子装包起运。接着，温家宝又登上一台“东方红”牌拖拉机的驾驶室，在司机的指导下挂挡、加油，紧握方向盘，稳稳地驾驶着，在刚刚收割的麦地里深耕、施肥，种下秋玉米。走下驾驶室时，温家宝幽默地向农民们问道：我的驾驶技术还合格吗？农民们回答道，很好！很好！他们热烈地鼓掌，麦田里洋溢着欢快的气氛。随后，温家宝还登上气象应急服务指挥车，与河南省气象台会商中心的工作人员通过视频对话，了解麦收期间的天气情况，勉励气象工作者为麦收提供优质、及时的气象服务。<br />
<br />
蔬菜供应和价格也是温家宝关心的问题。距许昌市区5公里许昌县将官池镇，天和万亩有机蔬菜示范基地正在建设中，目前种植面积已经达到5200亩。这是河南省规模最大的有机蔬菜生产基地。温家宝来到这里站在地头望去，菜地里碧绿的菜苗绵延不绝、生机勃勃。他走进一块菜地，蹲下身来与正在拔草疏苗的农民们聊天，了解农民们的收入、蔬菜土地流转情况。基地负责人介绍说，他们生产的蔬菜自然吹风、规模种植、绿色养护，出口香港、澳门。明年万亩有机蔬菜基地就会建成，并准备在当地销售。温家宝听后说，我们既要保“米袋子”，又要保“菜篮子”，还要让农民增收。10日下午，温家宝还考察了郑州市农产品物流配送中心，详细了解蔬菜价格和市场供应情况。<br />
<br />
<br />
9日晚，温家宝在长葛市主持召开农民和基层干部座谈会，并作重要讲话。温家宝指出，当前，要高度重视全面抓好“三夏”生产。一要切实组织好跨区机收，促进抢收抢打、精打细收，确保夏粮颗粒归仓。二要认真抓好夏种，下功夫抢抓农时，及早安排好种子，做好机播准备，及时腾茬播种，千方百计扩大种植面积。三要加强夏季田间管理。坚持一种就管，搞好查苗补苗，及时追施肥料，加强病虫害防治，以早管促早发、促早熟。四要做好应对各种极端天气和自然灾害的准备，努力把灾害损失减少到最低限度。五要做好“三夏”保障工作，确保“三夏”生产柴油和化肥、农药、种子、农膜等生产资料供应。他强调，夏粮丰收为我们保障农产品供给奠定了更加坚实的基础。对今年农业形势，我们既要增强信心，又不能盲目乐观，要清醒地看到面临的严峻挑战，对可能出现的复杂情况要有足够的准备，继续做好政策支持、技术措施、市场调控等方面的工作，以百倍的努力争取全年农业有个好收成。<br />
