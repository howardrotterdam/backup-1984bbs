													<h2>成缺管博客：60年国庆 我哭了</h2>
						
						
																											<div id="postmessage_271159" class="t_msgfont">这两天可以说是我这些日子最忙碌的两天了，昨天一早六点多拍戏，然后赶去领取中国海外交流协会授予我的海外常务理事。之后又赶回剧组拍戏，拍完戏匆匆忙忙回去，收拾好衣物，赶到集合的酒店，因为今天早上要乘坐统一的大巴士往天安门。到了酒店忙碌了一天突然没事情做了，躺在床上，怎么也睡不着。好不容易1点半能睡下了，猛的一睁眼2点55分，再一睁眼5点27，又醒来了，不行不行，干脆不睡了。坐在那我就想，阅兵吗，不关我事呀，我就是去观礼台的，怎么闹的自己这么紧张呢。打开窗帘看看天，还有些朦朦的，洗个澡回来再看，嘿，大晴天啦。我们的国家现在太强大了，太好了，连老天都帮我们。<br />
<br />
今天上午看阅兵，到了天安门，哇，我真是太幸运了，居然让我坐在了观礼台的第一排，离马路非常的近。<br />
<br />
演奏国歌之前，整个天安门广场鸦雀无声，听到“全体起立，唱国歌”的时候，我就听见“哗”，人们全都站了起来，当国歌响起的时候，哇，整个天安门广场上30万人都一起高唱国歌，那个声音已经把军乐团的声音都盖过了，在那一刹那我的眼泪情不自禁地流了下来。<br />
<br />
当我看到我们的主席的车开过来我当时真的非常激动，都有上前和他握手的冲动。<br />
<br />
再看到一个一个方阵的官兵威风的迈着整齐的步伐走过广场的时候，那种震撼的感觉是无法用语言来形容的。这些士兵起码都训练了8个月的时间，为了今天这短短的几分钟，他们流了多少汗水呀，付出多少辛苦呀，可他们带给我们的是作为一个中国人的荣誉、作为一个中国人的骄傲。<br />
<br />
晚上的国情大联欢，导演组安排我唱歌的地方好特别，在人民大会堂的楼顶上。哇，今天我拍了好多照片呢，短短的十分钟把我的相机就拍满了，真的很感谢导演组给我这么好的一个机会。而且今天安排我唱《国家》这首歌我觉得真的很恰当，这么多年走过来，我是太有感受了。先是在国外拍戏感受到外国人对我们中国、对我们中国人看法的变化；之后是在香港没有回归之前，中国大陆对我们来讲很神秘，一回归自己马上变成一个真真正正的中国人。而且回归之后的十年，看着我们的国家每天都有新变化，可以说是翻天覆地的大变化，有喜、有乐、有悲、有痛，真是什么都有了，眼看着我们的国家变的更加强大，真的让我感觉“国家是由我们千千万万的家组成的，有了强的国，我们才有富的家”。像今天这样的大事是由我们国家来做的，对于我们每个人来讲，我们要为我们的国家做些力所能及的小事情，比如做慈善呀，比如到国外去宣传我们国家的文化呀，等等。我知道我不是专业的歌手，但是每次我唱起《国家》这首歌的时候，我都会唱出我心中对国家的那种感觉。<br />
<br />
今天我们祖国60岁的生日，真的是举国欢腾，也让我深切的感受到作为一个中国人太兴奋太骄傲了。我们这一代人太幸运了，我们赶上了中国过往这几千年来最辉煌的时代。到现在，国旗、国徽、国庆60周年的三个胸章还一直被我别在衣服上；游行队伍的欢呼声还回响在我的耳边、威武的军队方阵那整齐划一的步伐还萦绕在我的脑海；国旗升起的那一刹那的那种感动还贯穿在我的心中……<br />
<br />
祝福我们的祖国吧，希望我们的祖国风调雨顺，蒸蒸日上。让我们每个人都把国家放在我们的心里，去感受作为一个中国人的骄傲和自豪吧。<br />
<br />
我爱你！中国！<br />
