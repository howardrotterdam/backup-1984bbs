<br />
<strong>　　编者按：被索马里海盗劫持了近2个多月的中国“德新海”号货轮终于获救了。根据外交部网站公布消息，北京</strong><a href="http://tool.forex.com.cn/other/worldtime.htm" target="_blank"><strong>时间</strong></a><strong>12月28日3时，中国“德新海”轮安全获救，全员25人平安，中国海军护卫航舰已将“德新海”轮保护至安全海域。另据报道，索马里海盗称，周日一直升飞机向“德新海”轮抛掷了400万美元赎金。</strong><br />
<br />
　　2009年10月19日15时20分，青岛远洋运输公司所属的中国籍散货轮“德新海”轮在印度洋被海盗武装劫持，事发地远离中国护航编队A汇合点南偏东1080海里。<br />
<br />
　　“德新海”轮是在装载煤炭，由南非开往印度航次中，在塞舌尔岛东北320海里、摩加迪沙东偏南980海里处(南纬1度49分3.9秒，东经60度4分49秒)被武装海盗分子劫持。该轮与公司的最后通话时间为北京时间19日16时35分。事发地点远离中国护航编队A汇合点南偏东1080海里。<br />
<br />
　　青岛远洋运输公司所属的散货轮“德新海”轮在印度洋被海盗武装劫持后，青岛远洋运输公司对“德新海”轮船员家属安抚工作马上进行。截止到10月20号，遭劫持的“德新海”轮25名船员身份全部确认。<br />
<br />
　　据青岛远洋运输公司介绍，“德新海”轮有25名船员，全部由青岛远洋运输公司派出，全部为中国籍船员。22名为山东籍，其中青岛籍16名，另外三名船员中，1名辽宁籍，1名河北籍，1名江苏籍。<br />
<br />
　　据青岛远洋运输公司20日介绍，2009年10月19日15时20分，青岛远洋运输公司所属的“德新海”轮报告有武装海盗登轮，之后失去联系。19日当晚，青岛远洋运输公司成立“德新海”轮应急工作小组，迅速开展船员家庭和家属工作。</div>

							
							
							
							
