　　中新网成都7月2日电 针对网上流传的“汉旺新镇安居房倒塌”一事，四川省绵竹市人民政府新闻办公室2日上午发布相关新闻称，6月30日下午5时，四川弘扬实业集团有限公司对正在自建的汉旺新镇弘扬馨嘉苑C2区27号楼第三单元质量问题安居房实施大型机械推挖式拆除，致使不明真相群众产生“安居房倒塌”错觉。<br />
<br />
　　据介绍，弘扬馨嘉苑C2区27号楼工程为汉旺新镇安居房工程，为砖混结构设计，4层3单元，建筑面积1925.32平方米，其中第三单元建筑面积641.7平方米，共8套。根据四川省灾后重建和安居房修建的相关政策，四川弘扬实业集团有限公司为该项目的建设单位。该项目为该集团自筹资金自主开发、自主销售的安居房。该楼于2010年3月开工，截止6月中旬已施工到四层砌体。<br />
<br />
　　6月24日，绵竹市建设行政管理部门在例行检查过程中，发现该工程第三单元出现垂直度偏差异常，施工单位也发现了这个问题。随即，绵竹市建设行政管理部门和汉旺镇政府紧急召集工程建设、勘察、施工、监理单位和邀请有关专家到现场进行了细致的检查鉴定，形成地基加固或拆除后重建两种方案。本着确保质量、对群众负责的态度，绵竹市建设行政管理部门责令建设单位对出现问题的建筑物进行安全拆除重建。<br />
<br />
　　事件发生后，德阳、绵竹两级党委、政府高度重视，要求有关部门对现场进行有效控制，以确保过往群众安全，责成建设、安监等部门对滞留现场的群众进行说明解释，澄清事实，解答疑惑。并组织专家对建筑物出现的垂直度偏差异常的原因进行科学鉴定，同时对该建设单位在建的所有建筑物进行安全质量检查，确保群众住上安全的住房。(完)</div>

							
							
							
							
