																											<div id="postmessage_727484" class="t_msgfont">四川汶川大地震重灾区绵竹市汉旺镇一座新建的四层楼房，1日突然整体垮塌，遭质疑是豆腐渣工程。但官方辩称，塌楼是建筑商发现楼房有问题主动拆除的。 <br />
<br />
<br />
　　有网民以《汉旺新房被震垮，现场堪比五一二》为题发帖称，绵竹汉旺镇新建安居房基本完工的C区二十七栋，1日突然整体垮塌，「没有发生大余震，也没有暴雨，房子说垮就垮，而且垮的干净彻底，这不是在堆积木嘛？」据《南方都市报》报道，一些亲历现场的当地居民表示，该房屋倒塌时景况与两年前该地因地震而损毁的房屋极为相似，又因倒塌时当地刚下过暴雨，关于其品质的质疑声颇多。 <br />
<br />
<br />
　　帖子透露，汉旺镇新建安居房坐落于汉旺镇新城，距老城几公里。援建方是江苏省，建筑承包方是四川德阳市弘扬公司。知情人士分析，垮塌原因包括地基没有挖到基石就匆忙开建、施工用的钢筋太细小、水泥没达标等。在网络上，即刻被称作「史上最牛豆腐渣工程」。 <br />
<br />
<br />
　　对此，承建单位德阳弘扬实业公司与绵竹市政府2日均发出公告，称该安居房并非垮塌，而是由于存在略微倾斜的问题，所以被相关单位主动拆除。由于并未向当地居民通报，进而致使「不明真相民众产生安居房倒塌错觉。」楼房倾斜的原因，绵竹市委宣传部称可能是地质原因导致，目前还在调查。同时，绵竹市建设局也澄清，该安居专案并不属于灾后国家援建项目，也不是江苏省无锡援建的项目，而是由德阳弘扬实业公司自投资金、自主开发、自行销售的专案，当地政府部门对其仅有一定的监管作用。 <br />
<br />
<br />
　　「自拆」未提前发布通知 <br />
<br />
<br />
　　绵竹市建设局也对外称，6月30日下午5时，施工单位选择了施行大型机械推挖的方式进行拆除。但据当时围观的居民讲，7月1日晚上8时左右，政府官员到来，并有「四辆公车载着特警」随后而来，此后，特警对现场人员进行了清理，这时，才有大型的挖掘机来到现场开始清理现场。报道指，对于「自拆」行为，无论是弘扬实业公司还是当地政府，均未提前发布通知。 <br />
<br />
<br />
　　当地居民：问题早已出现 <br />
<br />
<br />
　　发生问题的安居房为砖混结构设计，4层3单元，建筑面积1925.32平方米，其中第三单元建筑面积641.7平方米，共8套。当地多名居民表示，在楼房倒塌之前，他们曾多次向当地政府反映相关问题，但都没有得到正面答复。居民王某说，他们预先交付两万元的定金购买该处房子，「住板房已经两个秋冬，等待住新房眼巴巴望了两年，虽然只垮了一栋，但是，这样品质的房子，谁敢去住？」 <br />
<br />
<br />
　　6月26日，一些购买安居房的居民曾集体看过房子，有几栋房子凭肉眼就可以看得到倾斜。 6月29日，他们再次发现问题并找到当地政府，希望可以得到政府的帮助，王某称，当时一名有关负责人曾以人格担保说房子的品质绝对没有问题。 <br />
<br />
<br />
　　弘扬实业公司工地一名工人透露，弘扬的房子与江苏援建的房子相比，「品质实在太撇(差)了」。在江苏援建工地，围绕圈梁浇筑的混凝土厚度一般是20公分左右，但在弘扬只有10公分。(来源于网媒)</div>

							
							
							
							
