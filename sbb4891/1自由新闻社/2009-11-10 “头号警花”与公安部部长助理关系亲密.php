																											<div id="postmessage_322881" class="t_msgfont">======“头号警花王菲”这个词条现在已经被百度和谷歌中国屏蔽了，他们为什么连一个小小警花都怕的要死？======<br />
<br />
　　历时近一年，“问题首富”黄光裕系列案审判渐次展开。作为此案枝节的原上海市公安局长宁分局副局长钱宏祥受贿案，揭开了系列案之冰山一角。<br />
<br />
　　“头号警花”春节前“落马”<br />
<br />
　　9月18日9时许，钱宏祥被控受贿案在上海市第一中级法院开庭审理。48岁的钱宏祥虽然只是一名正处级警员，却是改革开放以来上海公安系统走向公开审判席的最高级别干部。钱宏祥仅被指控受贿一项罪名，贿金24万元。一周后，钱宏祥一审领刑七年。他选择了上诉。<br />
<br />
　　此案主角落寞，却有赫赫有名的配角陪衬――在钱宏祥受贿案中充当掮客的，是原公安部政治部处级干部王菲。<br />
<br />
　　此王菲原为一名部队文艺干事，后转业进入广东省汕头市公安局。经原公安部部长助理郑少东等人援手，王菲被提拔至公安部政治部。公开资料显示，相貌出众的王菲曾担纲历年来公安部晚会的主持人，有“头号警花”之称。<br />
<br />
　　据记者调查，郑少东曾由广东省公安厅常务副厅长调任公安部，与王菲关系甚密。2009年1月，郑少东因黄光裕案被“双规”审查，王菲也于今年春节前夕应声“落马”。大量线索显示，王菲宛若“无间行者”，混迹于京沪粤等地的多宗贿案中。<br />
<br />
　　接近案件的人士告诉记者，年过三十的王菲，目前仍由郑少东案专案组实施“双规”审查。<br />
<br />
　　网络赌博案侦查时受阻挠<br />
<br />
　　钱宏祥于2009年3月被纪检部门调查。钱主动交代了有关情况。8月，他被移送审查起诉。2008年8月，他利用职务便利接受王菲请托，释放一名已被刑拘的犯罪嫌疑人，并改为行政处罚。钱因此收受王菲转送的财物总价达24万余元。<br />
<br />
