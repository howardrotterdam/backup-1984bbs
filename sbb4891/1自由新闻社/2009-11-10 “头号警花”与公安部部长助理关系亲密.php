													<h2>“警花”王菲掮客档案 (财经最新被河蟹文章)</h2>
						
						
																											<div id="postmessage_321552" class="t_msgfont">百度和谷歌已经屏蔽警花的搜索.<br />
<br />
财经原文如下:<br />
<br />
两名正处级警员钱宏祥、王菲的“捞人”生意，揭示黄光裕系列案关系网一角<br />
<br />
《财经》记者 陈中小路 罗昌平<br />
&nbsp;&nbsp;<br />
　　历时近一年，“问题首富”黄光裕系列案审判渐次展开。作为此案枝节的原上海市公安局长宁分局副局长钱宏祥受贿案，揭开了系列案之冰山一角。<br />
　　9月18日上午9时许，钱宏祥被控受贿案在上海市第一中级法院开庭审理。钱宏祥虽然只是一名正处级警员，却是改革开放以来上海公安系统走向公开审判席的最高级别干部。<br />
　　钱宏祥仅被指控受贿一项罪名，贿金24万元。当日庭审低调，其家属及司法系统官员等十余人到场旁听。一周后，钱宏祥一审领刑七年。他选择了上诉。<br />
　　此案主角落寞，却有赫赫有名的配角陪衬――在钱宏祥受贿案中充当掮客的，是原公安部政治部处级干部王菲。<br />
　　此王菲原为一名部队文艺干事，后转业进入广东省汕头市公安局。<br />
　　经原公安部部长助理、经侦局局长郑少东等人援手，王菲被提拔至公安部政治部。公开资料显示，相貌出众的王菲曾担纲历年来公安部晚会的主持人，有“头号警花”之称。<br />
　　据《财经》记者调查，郑少东本是广东潮汕人士，曾由广东省公安厅常务副厅长调任公安部，与王菲关系甚密。2009年1月，郑少东因黄光裕案被“双规”审查，王菲也于今年春节前夕应声“落马”。大量线索显示，王菲宛若“无间行者”，混迹于京沪粤等地的多宗贿案中。<br />
　　接近案件的人士告诉《财经》记者，年过三十的王菲，目前仍由郑少东案专案组实施“双规”审查。<br />
事发网络赌博案<br />
　　2009年10月，钱宏祥面对一审判决书度过了他的48岁生日。钱长期在公安系统工作，由水警入行；2003年起担任上海市公安局长宁分局副局长一职，分管治安、特警、人口办、看守所等重要领域的工作，直至案发。<br />
　　钱宏祥于2009年3月被纪检部门调查。相关司法材料称，3月初，公安部纪委、上海市纪委责成上海市公安局纪委成立专案组，找钱宏祥谈话。钱主动交代了有关情况。<br />
　　4月，钱宏祥案接连完成刑拘和批捕手续，进入司法程序。由上海市检察院第一分院反贪局侦查终结，于8月移送审查起诉。<br />
　　钱宏祥被控罪名只有一条――2008年8月，在他分管的一起案件查处过程中，利用职务便利接受王菲请托，释放一名已被刑拘的犯罪嫌疑人，并改为行政处罚。钱因此收受王菲转送的财物总价达24万余元。<br />
　　这宗案件，便是涉及上海、广东、台湾和香港四地的“720”网络赌博案。<br />
　　2008年7月，上海市公安局长宁分局对一起网络赌博案收网，抓获20多名犯罪嫌疑人，其中包括一个名叫肖春燕的女性。肖是在广东某银行替赌博组织进行取款转账时被抓获的“马仔”。据她到案后交代，她受香港朋友“阿东”委托，进行取款和转账，自己并不知道这一行为与网络赌博有关。<br />
　　这起网络赌博组织的幕后“庄家”指向台湾和香港，据称与香港黑帮人物连超的洗钱网络存在交叉。连超亦是广东潮汕人，与黄光裕、郑少东系同乡，三案直接关联。<br />
　　多方知情人士透露，身陷此宗网络赌博案的赌客多为“有身份的人”，故而案件侦查期间受到多方阻扰，王菲亦是出手施力者之一。<br />
　　当时，王菲找到了上海市公安局长宁分局的分管领导钱宏祥，要他“帮忙”和“放人”。她要解救的，正是肖春燕。<br />
<br />
中间人王菲<br />
　　王菲介入这起网络赌博案，则因该案与其庞杂的社交圈存在交集。根据检方宣读的证词片段，王菲自称涉案嫌犯肖春燕是其长江商学院同学安俊宇的“好友”。<br />
　　据一位同学介绍，王菲2007年起入读长江商学院11期EMBA上海班，还被邀请主持该学院的晚会。<br />
　　知情人士表示，王菲虽然常到上海，却很少出现在长江商学院课堂上。她经常现身于五星级宾馆的各类豪华饭局，并结识各方政商界人士。<br />
　　2008年7月的一天，王菲拨通了钱宏祥的电话，打听肖春燕的情况。在知道肖已被刑拘后，王直截了当地说：“你们赶快把她放了。”这种说法当时让为官多年的钱宏祥吓了一跳，认为她“口气蛮大”。随后，钱回答称这个案子很大，已经报到市局，不可能放人。王菲则表示，能帮忙就尽量帮忙。钱表示答应。<br />
　　根据王菲的证词，受肖春燕的朋友安俊宇请托后，她亲自跑到上海两次约见钱宏祥。第一次，王菲在其下榻的五星级宾馆上海千禧海鸥酒店内，送给钱一个知名珠宝品牌“卡地亚”礼盒，内有一款价值3.8万元的女式手表，以及价值分别为约3500元的皮带和2800元的丝巾各一份，并称这是送给钱宏祥妻子的“小礼物”。<br />
　　经钱宏祥的运作，肖春燕在刑拘期满后“因证据不足”未被批捕，但没收其380万元赃款。对此，钱宏祥在法庭上强调，由于背后的“庄家”没有抓到，肖到案后拒绝供述犯罪行为，确实存在证据不足的问题。<br />
　　钱宏祥的律师易国勇辩称，同案被抓的嫌犯中，除了被起诉的几名主犯，获释者还包括其他部分嫌犯，而非肖春燕一人。<br />
　　不过，庭审显示，在肖春燕案办理过程中，王菲曾多次致电钱宏祥，催其办理相关事宜，大到涉案账户的解冻，小到肖春燕的替换衣服，王菲都亲自过问。<br />
　　在肖被释放后，王菲再次来沪约见钱宏祥，在上海千禧海鸥酒店内，塞给钱宏祥一个自称是“北京特产”的塑料袋。钱宏祥离开后，在车上发现，塑料袋内装有人民币20万元。<br />
　　钱宏祥表示，自己当时就致电王菲，说“你不要害我”，要求退还礼物和钱财，但王菲以自己已经休息为由婉拒。钱再联系时，王菲自称已回北京。<br />
　　此后，钱宏祥将这笔款项放在办公室，打算归还。但两人再未见面，还钱之事不了了之。钱宏祥到案后供认，他事后曾向具体负责查办此案的下属表示，“公安部领导”为此案中的肖春燕打了招呼。<br />
　　按照钱宏祥的说法，他在与王菲的多次言谈中，知道她“认识很多领导”，对她敬畏三分。<br />
　　“警花”王菲一举成名，始于公安部2002年春节晚会。在这个由央视一套直播的节目中，王菲与央视主持人联袂主持。彼时，她还是汕头市公安局的一名普通警官。<br />
　　生于北国的王菲于2000年从沈阳军区转业到汕头市公安局宣传科。王在部队期间从事文艺工作，声音甜美，能歌善舞；转业后，曾主持过汕头电视台的《汕头警声》、广东省电视台的《与法同行》等节目。<br />
　　据《汕头都市报》报道，王菲2002年6月代表汕头市公安局参加省公安厅主办的“三项教育”演讲比赛，以该省第一的成绩作为惟一代表参加了公安部的演讲比赛。熟知其人者告诉《财经》记者，王菲彼时即和郑少东相熟，并一直保持亲密关系。<br />
　　2005年4月，郑少东晋升为公安部党委委员、部长助理，王菲随即被调入公安部。<br />
　　据介绍，王菲喜好奢侈品牌，在北京时出入豪宅，个人拥有不菲身家。而郑少东的北京邻居介绍，郑之妻子已病逝，其膝下有一子。<br />
<br />
不见行贿人<br />
　　庭审显示，上述20万元现金贿款的来源，仅是50万元“捞人费”的一部分。这笔钱经安俊宇、钱义浩转至王菲手里。安、钱两人均为中间人。<br />
　　王菲的EMBA同学安俊宇，为青岛一家投资公司的老板。除了山东省红十字会的一份捐款名单上有此同名，公开资料里罕见其信息。<br />
　　关于钱义浩，司法材料里，他自称为“个人”身份。但多方知情人士透露，此人在上海染指多家大型高档休闲娱乐场所，如闵行区的“碧海连天”、嘉定区的“天上人间”，均与之关系密切。<br />
　　钱义浩还是王菲和钱宏祥共同的朋友。据钱宏祥回忆，2007年末，正是钱义浩把王菲“带过来吃饭”，并介绍给他认识。第一次见面时王菲称自己是来上海读书的，两人当时留下了各自的手机号。<br />
　　上述50万元现金，除了给钱宏祥的20万元，从中穿针引线的王菲截留了绝大部分。庭审显示，在王菲第一次约见钱宏祥之前，她和安俊宇在上海恒隆广场购买礼品，除了送给钱宏祥的钱物，王还购买了价值十余万元的两款男式“卡地亚”手表。<br />
　　钱宏祥案庭审中，王菲未被列为同案被告。钱的辩护律师易国勇在法庭上欲谈及相关财物的用途时，被审判长制止。<br />
　　而钱宏祥案的行贿方仍然扑朔迷离。安俊宇的证词称，这笔款项来自其台湾的朋友。检方调查的银行记录则显示，款项来自一个西安的身份证号码，但这个身份证号的主人并未被调查。在法庭上，易国勇律师对钱宏祥作了无罪辩护，并指出，这起案件连行贿人都没有，存在明显漏洞。<br />
　　今年3月，钱宏祥被纪检部门约谈后，上缴了总计24万余元的财物。<br />
　　对于为何迟迟不归还或上缴这些财物，钱宏祥和其辩护律师均强调，因为王菲“身份特殊”，钱宏祥感觉压力很大；虽然早有退回赃款的打算，但既要顾及王菲的情面不能硬退，又不敢去向组织上缴。<br />
　　9月25日，钱宏祥案一审宣判，检方指控获得法院全部认定，钱宏祥被判处有期徒刑七年。此后他提出上诉，目前该案尚未二审。■</div>

							
							
							
							
