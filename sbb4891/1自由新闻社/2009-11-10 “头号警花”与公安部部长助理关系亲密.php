													<h2>“头号警花”与公安部部长助理关系亲密</h2>
						
						
																											<div id="postmessage_321200" class="t_msgfont"><img src="http://news.sznews.com/images/attachement/jpg/site3/20091110/0019b90e16d60c6301553a.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
历时近一年，“问题首富”黄光裕系列案审判渐次展开。作为此案枝节的原上海市公安局长宁分局副局长钱宏祥受贿案，揭开了系列案之冰山一角。<br />
<br />
“头号警花”成名于公安部春晚<br />
<br />
“警花”王菲成名，始于公安部2002年春节晚会。在这个由央视一套直播的节目中，王菲与央视著名主持人联袂主持。彼时，她还是汕头市公安局的一名普通警官。<br />
<br />
生于北国的王菲于2000年从沈阳军区转业到汕头市公安局宣传科。王在部队期间从事文艺工作，声音甜美，能歌善舞;转业后，曾主持过汕头电视台的《汕头警声》、广东省电视台的《与法同行》等节目。<br />
<br />
据《汕头都市报》报道，王菲2002年6月代表汕头市公安局参加省公安厅主办的“三项教育”演讲比赛，以该省第一的成绩作为惟一代表参加了公安部的演讲比赛。熟知其人者告诉记者，王菲彼时即和郑少东相熟，并一直保持亲密关系。<br />
<br />
2005年4月，郑少东晋升为公安部党委委员、部长助理，王菲随即被调入公安部。<br />
<br />
据介绍，王菲喜好奢侈品牌，在北京时出入豪宅，个人拥有不菲身家。<br />
<br />
“头号警花”春节前“落马”<br />
<br />
9月18日9时许，钱宏祥被控受贿案在上海市第一中级法院开庭审理。钱宏祥虽然只是一名正处级警员，却是改革开放以来上海公安系统走向公开审判席的最高级别干部。钱宏祥仅被指控受贿一项罪名，贿金24万元。一周后，钱宏祥一审领刑七年。他选择了上诉。<br />
<br />
此案主角落寞，却有赫赫有名的配角陪衬――在钱宏祥受贿案中充当掮客的，是原公安部政治部处级干部王菲。<br />
<br />
此王菲原为一名部队文艺干事，后转业进入广东省汕头市公安局。<br />
<br />
经原公安部部长助理郑少东等人援手，王菲被提拔至公安部政治部。公开资料显示，相貌出众的王菲曾担纲历年来公安部晚会的主持人，有“头号警花”之称。<br />
<br />
据记者调查，郑少东本是广东潮汕人士，曾由广东省公安厅常务副厅长调任公安部，与王菲关系甚密。2009年1月，郑少东因黄光裕案被“双规”审查，王菲也于今年春节前夕应声“落马”。大量线索显示，王菲宛若“无间行者”，混迹于京沪粤等地的多宗贿案中。<br />
<br />
接近案件的人士告诉记者，年过三十的王菲，目前仍由郑少东案专案组实施“双规”审查。<br />
<br />
网络赌博案侦查时受阻扰<br />
<br />
48岁的钱宏祥长期在公安系统工作，由水警入行;2003年起担任上海市公安局长宁分局副局长一职，分管治安、人口办、看守所等重要领域的工作，直至案发。<br />
<br />
钱宏祥于2009年3月被纪检部门调查。相关司法材料称，3月初，公安部纪委、上海市纪委责成上海市公安局纪委成立专案组，找钱宏祥谈话。钱主动交代了有关情况。8月，钱被移送审查起诉。<br />
<br />
钱宏祥被控罪名只有一条――2008年8月，在他分管的一起案件查处过程中，利用职务便利接受王菲请托，释放一名已被刑拘的犯罪嫌疑人，并改为行政处罚。钱因此收受王菲转送的财物总价达24万余元。<br />
<br />
这宗案件，便是涉及上海、广东、台湾和香港四地的“720”网络赌博案。<br />
<br />
2008年7月，上海市公安局长宁分局对一起网络赌博案收网，抓获20多名犯罪嫌疑人，其中包括一个名叫肖春燕的女性。肖是在广东某银行替赌博组织进行取款转账时被抓获的“马仔”。<br />
<br />
这起网络赌博组织的幕后“庄家”指向台湾和香港，据称与香港黑帮人物连超的洗钱网络存在交叉。连超亦是广东潮汕人，与黄光裕、郑少东系同乡，三案直接关联。<br />
<br />
多方知情人士透露，身陷此宗网络赌博案的赌客多为“有身份的人”，故而案件侦查期间受到多方阻扰，王菲亦是出手施力者之一。当时，王菲找到了上海市公安局长宁分局的分管领导钱宏祥，要他“帮忙”和“放人”。她要解救的，正是肖春燕。<br />
<br />
“口气蛮大”的中间人王菲<br />
<br />
王菲介入这起网络赌博案，则因该案与其庞杂的社交圈存在交集。根据检方宣读的证词片段，王菲自称涉案嫌犯肖春燕是其长江商学院同学安俊宇的“好友”。<br />
<br />
王菲2007年起入读长江商学院11期EMBA上海班，还被邀请主持该学院的晚会。知情人士表示，王菲虽然常到上海，却很少出现在长江商学院课堂上。她经常现身于五星级宾馆的各类豪华饭局，并结识各方政商界人士。<br />
<br />
2008年7月的一天，王菲拨通了钱宏祥的电话，打听肖春燕的情况。在知道肖已被刑拘后，王直截了当地说：“你们赶快把她放了。”这种说法当时让为官多年的钱宏祥吓了一跳，认为她“口气蛮大”。<br />
<br />
根据王菲的证词，受肖春燕的朋友安俊宇请托后，她亲自跑到上海两次约见钱宏祥。第一次，王菲在其下榻的五星级宾馆上海千禧海鸥酒店内，送给钱一个知名珠宝品牌“卡地亚”礼盒，内有一款价值3.8万元的女式手表，以及价值分别为约3500元的皮带和2800元的丝巾各一份，并称这是送给钱宏祥妻子的“小礼物”。<br />
<br />
经钱宏祥的运作，肖春燕在刑拘期满后“因证据不足”未被批捕，但没收其380万元赃款。<br />
<br />
在肖被释放后，王菲再次来沪约见钱宏祥，在上海千禧海鸥酒店内，塞给钱宏祥一个自称是“北京特产”的塑料袋。钱宏祥离开后，在车上发现，塑料袋内装有人民币20万元。<br />
<br />
按照钱宏祥的说法，他在与王菲的多次言谈中，知道她“认识很多领导”，对她敬畏三分。<br />
