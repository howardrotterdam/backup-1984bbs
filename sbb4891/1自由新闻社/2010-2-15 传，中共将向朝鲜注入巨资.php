<br />
<br />
韩国联合通讯社星期一（2月15日）报道引消息人士称，中国当局将向朝鲜注入巨额资金。<br />
<br />
韩联社引熟悉朝鲜官方大丰国际投资集团的消息人士说：“中国两三家大型银行和若干家跨国企业同大丰集团事实上已经结束了投资磋商作业。”<br />
