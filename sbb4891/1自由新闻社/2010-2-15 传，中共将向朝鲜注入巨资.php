这位消息人士还透露，这些资金“60%以上都来自中国”。<br />
<br />
有关的投资似乎主要涉及基础设施建设，韩联社报道提到的将得到资金注入的项目包括平壤至新义州的铁路建设、中国图们至罗先特别市铁路建设、平壤市内10万户住宅建设以及港湾建设等。据透露，有关的投资问题是在中国共产党中央对外联络部部长王家瑞本月早些时候访问朝鲜时最后敲定的。<br />
<br />
六方会谈<br />
<br />
韩联社报道指出，如果投资消息属实，这无疑会给今后的六方会谈重启努力以及朝鲜半岛局势带来重大影响。<br />
韩联社报道称有关投资约合朝鲜国内生产总值的70%。不过根据美国中央情报局2009年发布的数据，朝鲜的年度国内生产总值约为278亿美元。<br />
<br />
大丰国际投资集团是朝鲜在今年1月成立的，当时朝鲜还成立了国家开发银行。<br />
根据朝鲜官方的报道，大丰国际投资集团是朝鲜的对外经济合作机构，主要向朝鲜国家开发银行提供投资和融资。<br />
<br />
而朝鲜国家开发银行将建立现代的金融规范和体系，可同国际金融机构和国际商业银行进行交易，并根据国家政策开展投资业务。<br />
