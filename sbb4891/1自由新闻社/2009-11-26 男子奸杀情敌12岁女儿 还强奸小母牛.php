2009-11-26 　　星岛环球网消息：“吴志亮，你还想往哪里跑？”11月22日晚10时许，柳州市柳南公安分局10多名刑警，将出现在西江路一废弃厂房内的男子团团围控。14日凌晨，柳南区帽合村上木照屯一民房内，一名年仅12岁的女孩被人奸杀。案发后，柳南警方经过8天艰苦侦查，将这名叫吴志亮的男子抓获。一起骇人听闻的恶性杀人案，成功告破。<br />
<br />
　　午夜凶案<br />
<br />
　　父亲被锤伤 女儿被奸杀<br />
<br />
　　《南国今报》报道，14日凌晨3时40分许，柳南区帽合村上木照屯一栋民房2楼，36岁的女子阿秋和男友覃某正在熟睡。突然，梦中的覃某发出一声撕心裂肺的惨叫：一男子闯进房内，用锤子一样的东西，朝覃某头部死力捶了七八下。看得出，男子是想要覃某的命。阿秋迷糊中一看，眼前这名凶残的男子，就是她的前男友吴志亮。她本能地冲上去，想抢过吴志亮手中的作案工具，不料肩膀也遭到一锤猛击。吴志亮行凶后，扔下锤子，转身从2楼窗户跳下，消失在夜色中。覃某头部遭连捶，血流一地，阿秋急忙报警。随后，120急救医生赶到现场，阿秋陪同受重伤的覃某，坐上120急救车前往医院。<br />
<br />
　　事情发生后，同在附近租房居住的村民，对此事议论开来。事发2个多小时后，有村民认为，应该把覃某当晚的遭遇，通知覃某的家人。于是，有人上到覃某女儿在该民房租住的2楼房间（该房间跟覃某房间只隔着一个大厅），想问覃某12岁的女儿阿红（化名）要覃某家人的电话。当他们推门进入时，被眼前的一幕吓呆了：阿红“躺”在床上，身体被被子蒙住，下身没穿裤子。更可怕的是，阿红浑身冰凉，没了气息。<br />
<br />
　　疑犯原形<br />
<br />
　　性欲太旺盛 女友受不了<br />
<br />
　　当日凌晨5时50分，柳南警方接到报警后，迅速出警赶到现场。刑侦技术人员通过现场勘察，发现死者小红脖子上有明显掐痕，是被人掐害致死。12岁女孩惨忍被害，引起柳南公安分局领导高度重视，立即调集大批精干警力开展侦破工作。同时，柳州市公安局刑侦支队，也连夜派人前来增援。经过勘查，警方在案发现场找到了斧头、铁锤、菜刀和手电筒等物。警方初步判定，疑凶吴志亮是攀爬上覃某等人居住的二楼的；在袭击覃某之前，吴先潜入覃某12岁女儿小红房中，扼住小红喉咙使她窒息而死，并对她进行了性侵犯。<br />
<br />
　　阿秋说，今年7月，她因为生意上的来往，认识了今年45岁的吴志亮。吴志亮一直未婚，认识阿秋后不久，两人开始在谷埠街租房子，过起了同居生活。<br />
<br />
　　可是，同居才1个月，阿秋受不了了，因为吴志亮性需求过于旺盛，几乎每晚都要“行事”，甚至阿秋在煮饭做菜时，吴志亮也会要阿秋满足他，有时为了达到目的，还戴上假阳具。案发后，警方曾在两人的出租屋里查获不少春药。<br />
<br />
　　开始，阿秋还想忍一忍，不料一个月“折腾”下来，自己患上了妇科疾病。虽然吴志亮出钱，让阿秋把这病治好了，但阿秋不想再继续这样的生活，跟吴志亮提出分手。9月底，阿秋与帽合村上木照屯40岁的男子覃某，开始了一段新的恋情。吴志亮知道这件事后，认为覃某抢了自己的女友，心里很不爽快，曾忿忿不平地跟朋友抱怨：“自己医好了她，又不得用！”为此，吴志亮偷偷吊尾阿秋，知道阿秋跟覃某感情升温很快，并在帽合村上木照屯同居了。想到曾经在阿秋身上花的钱，吴志亮心里更是憋气，曾到覃某住处闹事，也遭到过覃某的警告：“阿秋已经跟我了，你就不要再来骚扰了！”<br />
　毫无人性<br />
<br />
　　无处可发泄 强奸小母牛<br />
<br />
　　阿秋的讲述，让民警对吴志亮生活轨迹，有了初步了解。而吴志亮也是前科累累，曾因盗窃等行为，4次被公安机关处理过。<br />
<br />
　　柳南警方赶到吴志亮家中，对吴的家人及亲戚朋友展开大量调查走访，得知吴志亮家在太阳村镇开有一家农庄，2008年3月吴志亮解除劳教归来，家里人让他在自家开的农庄内，摆摊卖些酸、玉米、烤红薯之类，一个月有5000多元收入，生活还算稳定。家人们满心盼望他改过自新，谁知吴不自爱，回来之后又做出一件令人震惊的事情。<br />
<br />
　　他解除劳教回来后，看到同村村民家中养了一头小母牛，欲火难抑的他潜入到牛栏中，找小母牛满足自己的性欲。当他心无旁骛地强奸小母牛时，被牛主人发现，以为吴是来偷牛的，便报了警。辖区派出所民警赶到，将吴志亮和小母牛的主人，带回派出所调解。经过和牛主人协商，吴为自己强奸母牛一事，掏出50元埋单，这才得到牛主人的原谅。<br />
<br />
　　此事传得沸沸扬扬。吴志亮家人得知他在外面做了如此伤风败俗的事情，都觉得蒙上了极大的羞辱。家人说，吴志亮性格狭隘，沉默寡言，平时在村子里，能说上话的人，都没几个。此外，警方在走访中获悉，案发后吴志亮潜逃在外，一直不见踪影。那么，朋友不多的吴志亮，到底潜逃到哪里呢？<br />
<br />
　　追凶八日<br />
<br />
　　百张查缉令 撒网擒恶徒<br />
<br />
　　掌握了这些情况，柳南公安分局下属的各刑侦大队、派出所全部行动起来，采取多种方式查找吴志亮的行踪。一方面，在媒体刊登协查信息悬赏追逃；另一方面，把查缉令贴到太阳村镇一带，提醒广大群众注意安全，及时为警方提供相关线索。案发后的8天时间内，气温骤降，民警冒着凄风冷雨，白天在太阳村一带搜山，夜晚则在村里各地点伏击守候，全力搜寻吴志亮的踪迹。<br />
<br />
