澳门是中华人民共和国的一个特别行政区，人口约为54.3万。根据澳门特别行政区宪法，即《基本法》(the Basic Law)，澳门除防务和外交事务外享有高度自治。在2004年最近一次的特别行政区领导人选举中，澳门特区行政长官何厚铧再次当选，连任5年。2005年，在总体上被认为自由和公正的选举中，选民通过分区直选选出了29人立法会中的12名议员。文官当局总体上对安全部队保持有效的控制。<br />
