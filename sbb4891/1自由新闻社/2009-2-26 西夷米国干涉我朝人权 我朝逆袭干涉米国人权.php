&nbsp; &nbsp; 《人权纪录》指出，黑人和其他少数族裔生活在美国社会的底层。据美国人口普查局２００８年８月公布的报告，２００７年全美家庭平均年收入为５０２３３美元，其中白人为５４９２０美元，拉美裔为约３８６７９美元，黑人为３３９１６美元，黑人和拉美裔家庭的年收入只相当于白人家庭年收入的６２％。 <br />
<br />
美国就业方面种族歧视司空见惯<br />
<br />
&nbsp; &nbsp; 中国国务院新闻办公室２６日发表的《２００８年美国的人权纪录》说，美国就业方面种族歧视司空见惯。<br />
<br />
&nbsp; &nbsp; 《人权纪录》引用美国劳工部的统计说，２００８年第三季度美国平均失业率为６％，其中白人失业率为５．３％，而黑人失业率为１０．６％，是白人的两倍。据美国机会均等委员会统计，２００７年该委员会共收到关于平等就业歧视方面的指控３０５１０件。<br />
<br />
美国女性收入为男性的78%<br />
<br />
&nbsp; &nbsp; 中国国务院新闻办公室２６日发表的《２００８年美国的人权纪录》说，美国妇女的平均收入低于男性。据美人口普查局２００８年８月公布的统计数字，美国全职女性２００７年的收入为３５１０２美元，男性为４５１１３美元，女性收入为男性的７８％。<br />
<br />
&nbsp; &nbsp; 《人权纪录》说，美国女性人口占全美总人口的５１％，但在目前第１１０届美国国会中，女议员有８８名，其中参议员１６名，占１６％；众议员７２名，占１６．６％。截至２００７年１２月，在美国州一级行政办公室任职的女性有７３人，占２３．２％；<br />
<br />
美国约有２亿支私人枪支　枪杀事件频发<br />
<br />
&nbsp; &nbsp; 中国国务院新闻办公室２６日发表的《２００８年美国的人权纪录》中指出，美国约有２亿支私人枪支，枪杀事件频发。<br />
<br />
&nbsp; &nbsp; 文章援引《基督教科学箴言报》２００８年６月２７日的报道说，美国最高法院做出裁决，明确个人有权持有和使用枪支。据统计，美国约有２亿支私人枪支，其中包括６０００万至６５００万支手枪。据《侨报》２００８年１０月１６日的报道，美国有４８个州规定公民有权携带枪支。<br />
<br />
美国暴力犯罪严重 平均每３１分钟发生一起谋杀案<br />
<br />
&nbsp; &nbsp; 中国国务院新闻办公室２６日发表的《２００８年美国的人权纪录》指出，美国暴力犯罪严重，危及公民的生命、财产和人身安全，平均每３１分钟发生一起谋杀案。<br />
<br />
&nbsp; &nbsp; 文章说，据美国联邦调查局２００８年公布的报告，２００７年，全美共发生约１４０万起暴力犯罪，１７０００多起谋杀案。９８０万起财产犯罪案件。其中，抢劫案４４５１２５件，比过去５年上升了７．５％。<br />
<br />
美国使用过大麻和可卡因的人数全球领先<br />
<br />
&nbsp; &nbsp; 中国国务院新闻办公室２６日发表的《２００８年美国的人权纪录》说，美国吸毒和自杀等社会问题严重。美国使用过大麻和可卡因的人数全球领先。<br />
<br />
&nbsp; &nbsp; 文章说，一项对１７个国家的５．４万人的调查表明，１６％的美国人曾服过可卡因，超过４２％的美国人承认曾吸食过大麻。<br />
<br />
&nbsp; &nbsp; 文章指出，美国中年白人自杀率逐年升高。２００８年１０月２１日发布的一项调查显示，在１９９９年至２００５年美国自杀率每年增长０．７％，４０岁至６４岁白人的自杀率每年增长２．７％。中年白人女性的自杀率上升了３．９％。 <br />
<br />
警察滥施暴力，侵犯公民权利<br />
<br />
&nbsp; &nbsp; 中国国务院新闻办公室２６日发表的《２００８年美国的人权纪录》说，美国警察滥施暴力，侵犯公民权利。<br />
<br />
&nbsp; &nbsp; 文章援引《芝加哥论坛报》２００８年６月的报道，芝加哥市在６月的两周内就发生８起警察枪击事件，其中５起致命。如在６月２２日，３９岁的清洁工沙佩尔・特雷尔在&nbsp;&nbsp;公寓楼道内被警察打死。６月２４日，芝加哥１８岁的路易斯・科隆在餐馆和朋友聚餐时，受到便衣警察例行盘查，科隆因害怕而逃跑，被警察击毙。 <br />
<br />
美国强奸发生率世界最高<br />
<br />
&nbsp; &nbsp; 中国国务院新闻办公室２６日发表的《２００８年美国的人权纪录》指出，美国妇女饱受家庭暴力和性侵犯之害。据统计，去急诊室看病的妇女中有１／３是由于家庭暴力所致。<br />
<br />
&nbsp; &nbsp; 《人权纪录》还说，美国针对原住民妇女的性暴力十分普遍。据大赦国际采访的一些原住民妇女们说，她们不知道在她们居住区还有哪个女人没有经历过性暴力。据统计，２００７年美国就业机会均等委员会共收到有关性骚扰方面的指控１２５１０件，其中８４％是女性提出来的指控。<br />
<br />
美国每年有大约180万儿童失踪报案<br />
<br />
&nbsp; &nbsp; 中国国务院新闻办公室２６日发表的《２００８年美国的人权纪录》引用有关报道说，美国每年有大约１８０万儿童失踪的报案，每年有超过３００万儿童被报案在身体、性、言辞、情感方面受到伤害、受到忽视、被遗弃甚至死亡，每年大约有１５００名儿童死于虐待。<br />
<br />
&nbsp; &nbsp; 《人权纪录》指出，美国儿童安全得不到保障。据美“保卫儿童基金”发表的２００８年度报告，仅在２００５年，就有３００６名儿童和青少年被枪杀。另据美国休斯敦大学儿童、法律、政策研究中心的调查，美国每天有８名儿童和青少年死于枪击，这相当于每４天就发生一次弗吉尼亚校园枪杀案，每３个小时就有１名儿童死于枪击。<br />
<br />
美国不断爆出虐囚丑闻<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;文章援引人权观察２００８年２月６日发表报告称，在关塔那摩监狱的多个“营地”，２７０个被拘留者中有１８５个被关在类似“超最大限度”监狱的场所，他们被剥夺了获得公正审判程序的权利。他们被极端限制与他人接触，一天２２小时在小囚室单独度过，仅有一点点或根本没有自然光和新鲜空气。<br />
<br />
美国在押囚犯数量居世界第一 基本权利难得保障<br />
<br />
&nbsp; &nbsp; 文章指出，美国囚犯的基本权利得不到保障。美国司法部８月公布的信息表明，１９９３年以来美国被法庭判刑入狱的人口比例增加，暴力犯罪人数占监狱总人数的一半以上。加利福尼亚州现有３３所监狱只可关押约８万人，但却关押着１５．６万名犯人，每名犯人平均生活面积仅为６平方英尺。<br />
