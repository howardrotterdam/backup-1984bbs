													<h2>美国国务院发布《2008年度各国人权报告》</h2>
						
						
																											<div id="postmessage_40362" class="t_msgfont">美国参考&nbsp; &nbsp; <a href="http://www.america.gov/mgck" target="_blank">http://www.america.gov/mgck</a><br />
