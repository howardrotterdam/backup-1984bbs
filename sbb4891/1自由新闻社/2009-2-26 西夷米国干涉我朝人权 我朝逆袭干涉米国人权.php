香港政府总体上尊重民众的人权，但一些核心问题依然存在。《基本法》的条款限制民众参与和更换政府的能力。关于新闻自我审查的指责继续存在。立法会提出或修改法律的能力受到限制，无权批准行政职务的任命。对妇女施暴的问题仍然受到关注。劳工面临一系列问题，包括最低工资和保障集体谈判权利的问题。<br />
