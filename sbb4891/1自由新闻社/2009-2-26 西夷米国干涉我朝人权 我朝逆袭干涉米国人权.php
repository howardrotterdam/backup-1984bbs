													<h2>杨恒均:美国不干涉中国人权了，我们自己干涉吧！</h2>
						
						
																											<div id="postmessage_40968" class="t_msgfont">美国国务卿希拉里访问中国，相当谦卑，相当友好，只谈经贸，不谈人权，让很多人跌破眼镜。其实早在年前我到美国折腾了一圈后就知道这个结果了，也写了文章。很多读者忧心忡忡，写信问我怎么看美国人不再那么关心中国人权这回事。<br />
<br />
<br />
<br />
我的回答很简单，美国人不关心我们的事，我们自己关心不就得了？美国人不干涉中国人权，我们自己干涉我们的“人权”嘛。再说，我们的人权为什么要美国人来干涉？<br />
<br />
<br />
<br />
美国人为啥不干涉中国的人权了？<br />
<br />
<br />
<br />
上次从美国回来，我已经就自己的观察写了一篇文章，我的结论很明确：美国不会再像以前那样干涉中国内政，也不会动不动就关心中国人权了。我提到了一些理由，这里再简单的补充一些：<br />
<br />
<br />
<br />
第一，美国干涉中国人权是出于利益还是价值观？这一直是研究中美关系的人紧追不放也始终不能也不应该一锤定音的争论。每个国家都希望或者坚持说自己的国家利益是符合秉持的价值观的，不会说一套做一套。但在实际操作中，要达到两者完全吻合几乎是不可能的任务。且不说美国人在中东支持君主国家这件事，就说他们对中国人权的关注，也不是完全受价值观支配的。<br />
<br />
<br />
<br />
例如上个世纪八十年代初，当时的中国人权怎么说也不比现在好吧？我就记得（1982年）因为在高中讲了我那地主爷爷解放前如何赌博和打土匪的祖传故事，而被随州市第一中学的团支部书记关在宿舍里不准上课达一个星期之久，理由是我宣传地主阶级的腐朽的生活方式。可是那个时候美国和中国打得火热，美国中央情报局的间谍进出中国如入无人之境，在我们的边境安装好几个监听站，我就从来没有听说过他们过问过中国人的人权。甚至对中国在建国后三十年里非正常死亡的几千万人的事，你也从来没有听到美国“干涉”过。<br />
<br />
<br />
<br />
从目前的国际局势来看，中美两国实在太互相需要了，发展紧密的合作关系符合两国的利益。特别是奥巴马，高谈阔论的演讲一篇接一篇，本身的当选又划了时代。不过，他比谁都清楚，美国人四年后如果还没有从危机中走出来，那么他奥巴马就得走人，刚刚创造了历史的奥巴马自己就立马成为历史了。<br />
<br />
<br />
<br />
第二，美国人也是清楚的，从长远来讲，只有不违背价值观的“利益”才是可以持久而不会带来恶果的。从这一点说，美国人只是改变了打交道的策略。实际上，美国人是务实的，美国内部一直存在着如何同中国打交道的争论，例如美国学者经常说的“面子”问题，我想不到的是美国政界对此也是很重视的。他们认为，同中国人打交道，你一味强硬，高高在上，不但达不到目的，而且很多时候适得其反。这道理很简单，如果我们想干涉邻居家一件不好的事，我们可以板着面孔去教训他们，也可以满面带笑去说服他们。要是你碰上一个来干涉你家庭事的邻居，你如何反应？你肯定会对板着面孔的人关上大门，而不得不听完笑脸邻居的解释。<br />
<br />
<br />
<br />
可是，正因为关注中国的人权符合美国的核心价值观，也是政治正确的，所以，无论是总统奥巴马还是国务卿克林顿，他们今后还得在国会和选民的督促下，偶尔板着脸干涉一下中国的内政，但主要是做给美国人看，对中国改善人权的实际作用微乎其微。<br />
<br />
<br />
<br />
第三，２００８年发生的一系列事件给美国人和西方社会深深上了一课。一开始中国搞奥运会和火炬传递，美国、法国等西方社会以为我们接轨了，竟然误把奥运会当一国际盛事，于是就对我们指手画脚，妄图施加一点影响，哪料到我们的小愤青们一拥而上，把法国人搞得措手不及，把美国人惊出一身冷汗。<br />
<br />
<br />
<br />
最近，一位美国国务院的小官员还神秘兮兮地对我说，米斯特杨（后来我纠正他应该叫我达克特杨――杨博士，嘿嘿），我相信你们对法国人的做法只是杀鸡儆猴而已，你们不敢对美国人大动干戈，但你们又想警告我们，于是，你们成功地用倒霉的法国的下场警告了美国等西方国家不要折腾中国，中国很愤怒。<br />
<br />
<br />
<br />
不管这哥们是在猜测还是在试探我的意思，经过奥运会与境外火炬传递一役，我们中国人虽然国际形象下降了，但却在精神上大获全胜，也让西方世界特别是美国终于承认：那奥运会既然轮到北京办，就是咱中国特色的奥运会，也就是中国的奥运会。<br />
<br />
<br />
<br />
还有一件更加重要的事就是奥运会让西方看到了中国的“民意”。既然中国老百姓民意已经觉醒了，既然在美国和法国的这种自由之地的中国留学生都自动支持中国政府，反对西方的霸权，所以，美国等西方国家以前一直自诩为为了中国人民而干涉中国内政的信念一下子无影无踪了。<br />
<br />
<br />
<br />
果然，我们看到，经过奥运会和境外火炬传递，美国和西方主流几乎都改弦易辙、不折腾了。无论后来的地震和毒奶粉事件，美国人和整个西方都保持了相当绅士风度的低调，是啊，美国人的孩子既不到四川读书，也不喝中国制造的毒奶粉，为什么要对中国的事指手画脚呢？更何况，虽然你们的牛奶质量低劣，你们学校宿舍不怎么牢固，但你们用来购买美国国债的玩艺可是真金白银啊。<br />
<br />
<br />
<br />
美国不干涉我们的人权，咋办？<br />
<br />
<br />
<br />
美国不干涉中国人权了，好多朋友好像很失望的样子，首先当然是一些右派和思想比较开放的读者。我想，这可能和一些左派愤青们长期在网络上使用的攻击语言有关，例如右派们一提到普世价值、人权等，左派们就跳起来，在愤青们的呐喊助威下，攻击右派们是汉奸、卖国贼，是美国特务，或者至少也拿了CIA（中央情报局）的一些好处费。这样攻击久了，连支持右派的一些读者们也或多或少地受到了一些影响，以为美国不关心中国人权了，右派们就失去了一些依靠。<br />
<br />
<br />
<br />
特别是在奥运会火炬传递时，网络上对于我们这些出来提出批评意见的作者的攻击一律以攻击美国和西方为背景，弄得一时之间连我自己也糊涂了，莫非我真成了美国的代言人？不过又一想，不对啊，这些左派坐在美国人发明的汽车里，噼哩啪啦打着美国人发明的电脑，用美国人发明的互联网，咋就没有想到他们早就是美国人的走狗呢？<br />
<br />
<br />
<br />
不过，这招对聪明的中国人不灵，但对脑子不怎么会转弯的美国人就很灵。别说还真有效果，从此以后，以美国为首的西方退缩了，对中国问题退避三舍。<br />
<br />
<br />
<br />
可是，中国互联网上的民意却不但没有退缩，反而更上一层楼。网络上对于层出不穷的侵害人权的批评日益热烈，如火如荼，所不同的是，这次的网络民意没有了西方的影子，多了草根的声音，以及不久前才对美国和法国愤怒不已的年轻人。<br />
<br />
<br />
<br />
现在该轮到左派们垂头丧气了。当我们的批评再次受到所谓左派的攻击的时候，我们明显的感觉到他们语气里失去了一些底气，因为他们现在面对的，不再是西方人的指手画脚，而是清一色的中国人，美国人不关心地震，不知道周老虎是什么，也不关心毒奶粉，对官员花费纳税人的钱到美国花天酒地基本上持欢迎态度，甚至不拒绝贪官污吏把人民的血汗钱全部转移到美国存进美利坚合众国的银行，或者购买旧金山的豪华别墅。<br />
<br />
<br />
<br />
到这次希拉里访华，闭口不提人权，如果说让右派们失望，还不如说让左派们失落，你去查看一下他们的文章就会发现，他们今后再也无法写出攻击普世价值是美国人和平演变中国、以及美国的代理人向中国推销的玩艺了。现在对他们谈普世价值和人权的是连美国在哪里都搞不清的农民工和受害孩子的母亲，当然还有那些逐渐被互联网启蒙的年轻网友们，包括几个月前被他们忽悠的愤青。<br />
<br />
<br />
<br />
我不知道大家是否注意到，２００８年的网络民意其实有一个大大的转向，在愤怒的青年们搞定了国际社会之后，他们在短暂的失去了目标后迅速找到了方向，让事故的负责官员们乖乖下台，把制造假奶粉的人绳之以法，人肉搜索贪腐官员，监督各级政府的所作所为等等，这些利益攸关的事儿显然比到家乐福抵制自己本来也买不起的法国货，以及连飞美国的机票都买不起却整天幻想要把红旗插到白宫的台阶上更加吸引了他们。<br />
<br />
<br />
<br />
于是，我们看到，当西方人不再干涉我们内政的２００８年下半年，我们中国人开始热衷干涉自己的“内政”，当西方人闭口不谈中国人权的时候，我们网民的嘴边却再也少不了人的权利。<br />
<br />
<br />
<br />
这就是网络的好处，当很多愤青们高声叫骂我的时候，很多朋友为我抱不平，但大家也看到，我从来没有回骂一句，为什么呢？因为当言论放开的时候，言论自由并不只是你和我的言论自由，所有的人都有言论自由，右派有，左派也一样应该有，不左不右的人同样也有。正因为如此，当言论自由在不太成熟的民族中生成的时候，一开始都会出现一些问题，有些还相当严重，然而，当言论自由不断普及开来而且深入人心的时候，你就会知道，人，只要是人类，在信息公开和意志自由的时候最终总能够选择正确的方向。你必须有信心，对自己有信心，对中国人有信心，因为中国人不但是人类的一部分，而且是很优秀的一部分。<br />
<br />
<br />
<br />
我们的人权，只有靠我们自己来“干涉”！<br />
<br />
<br />
<br />
我不知道大家注意到没有，在整个2008年，中国官方除了外交部对CNN提出一些质疑和警告之外，对于网络上所有批评政府的一些做法、揭露腐败现象、关心人权等等的自由的言论，一次都没有提出过像对CNN 那样的严厉批评。<br />
<br />
<br />
<br />
上海的余秋雨先生含泪劝告灾民不要被海外反华势力利用的时候，有人说他是在为政府说话，我觉得大家真是冤枉我们政府了，我们政府至少比余先生有良心一些，知道做错了，不会往人家美国人身上赖。<br />
<br />
<br />
<br />
大家可以回顾一下，迄今为止，政府高层发言人在任何公开的报告中说我们这些批评政府的网民是被反华势力利用的吗？当然我们的一些言论经常被删除，但也要看到还有一部分言论经常被“利用”也是事实，只不过是被中国政府“利用”来改善工、增进人权，例如在地震救灾中，在对毒牛奶的处理方法上，以及对一些腐败官员的人肉搜索、曝光公费出国旅游、网友对一些政策的建议、建言等等上，政府在很多时候几乎达到了我杨恒均的格言“从善如流”的地步。这不能不说是值得肯定的进步。<br />
<br />
<br />
<br />
我一直有一个观点，不承认落后，那么我们会越来越落后，但如果不认识到进步，那么我们就很难再进一步。中国政府在网络上的一些做法值得肯定，中国网民在网络上的言论自由之进步（特别是相比于传统媒体和现实社会），也是不容否定的，这当然和网络的特性有关（例如高科技的难以管理），然而，不能排除政府对网络管理的新思路。<br />
<br />
<br />
<br />
美国人在干涉中国人权的时候，很多时候说不到点子上，而且人权本应该包括很多方面（例如经济、社会和政治），而美国往往只注重在政治权力方面。说实话，在美国的经历，特别是和美国一些特别关注中国人权的美国人士的接触，有时让我感觉到他们只盯着最终的目标，却往往没有看到我们现在的位置。他们站在终点线上，灰心丧气地抱怨我们还离他们那么遥远，却不知道，我们已经离开了我们的起点线很远了，正在艰难地踽踽独行。<br />
<br />
<br />
<br />
2008年虽然是灾难年，也是近些年中国人失去了最多鲜活生命的一年，然而，却也是中国人更直接地认识到生命的价值和人权的意义的一年。其中不能不提宝宝在地震灾区的那句“是人民在养活你们”的大白话，以及胡哥的“以人为本”。<br />
<br />
<br />
<br />
这两句话对于中国人，也许比奥巴马一边大谈“你们松开拳头我们就握住你们的手”，以及一边奉劝人家“站在历史正确的一边”一边急忙把自己的国务卿送到历史的另外一边来握手拥抱要容易理解得多，也切实可行得多。<br />
<br />
<br />
<br />
无论是政府如何逐渐开放，无论互联网如何给我们提供了平台和机遇，如果我们的民众自己不关心自己的“人权”，就算美国人把他的全部舰队开过来，就算宝宝总理喊破了嗓子，也还是没有用的。<br />
<br />
<br />
<br />
我们必须学会去“干涉”本来属于我们自己的内政，以及关注我们自己的人权。</div>

							
							
							
							
