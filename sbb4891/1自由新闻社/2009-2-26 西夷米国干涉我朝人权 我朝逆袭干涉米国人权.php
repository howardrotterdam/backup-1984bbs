　　《人权纪录》指出，美国消极对待国际人权公约和国际人权领域的义务，在国际人权领域一贯顽固坚持双重标准，不履行国际人权义务，其向国外提供人道主义援助纪录与其世界头号富国地位极不相称。<br />
