美国国务院国际信息局(IIP)获悉，2月25日，美国国务卿克林顿(Hillary Rodham Clinton)出席国务院举行的新闻发布会，正式发布《2008年度各国人权报告》。以下是人权报告序言部份的译文。人权报告的中国部份(包括西藏、香港、澳门)和台湾部份的摘译另行刊载。<br />
