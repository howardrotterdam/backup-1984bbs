<br />
《2008年度各国人权报告》序言<br />
<br />
人类的进步取决于人类的精神。今天，这一不容回避的事实比以往任何时候都更鲜明，因为一个新世纪提出的种种挑战要求我们调动人类的一切才智，推动我国和整个世界向前迈进。<br />
<br />
保障每一位男性、女性和儿童全面参与社会并发挥其天赋潜能的权利，是建国以来一直激励着我国的理想。这个理想已经被纳入联合国的《世界人权宣言》(Universal Declaration of Human Rights)，并且在奥巴马总统的就职演说中得到体现。他当时要求我们谨记，每一代人都必须弘扬这个信念：&quot;人人平等，人人自由，人人都应享有追求全部幸福的机会&quot;。<br />
<br />
我国对外政策也必须增进这些永恒的价值观念。这些价值观赋予人民自主的权利，可以自由发表言论，自由思考，自由信仰宗教和自由集会，有尊严地做自己的工作，过自己的家庭生活，并相信他们憧憬的更光明的未来并非遥不可及。<br />
<br />
倡导人权是我国对外政策的一个关键环节。我们不仅要求在美国国土上实现我们的理想，而且将在同全世界其他国家和人民接触的过程中争取增进对人权的尊重。我们将通过政府会谈和官方对话展开部分工作，这对于促进这项事业具有重要意义。但是，为了抗击削弱人类精神，限制人类发挥潜力，阻碍人类进步的暴政和专制，我们不会仅仅依赖于这一种方式。<br />
<br />
我们将使之成为一项不仅仅限于政府的全球性努力。我们将联合非政府组织、工商企业、宗教领袖、中小学和高等院校，以及公民个人──他们所有的人都为创造一个让人权得到承认、尊重和保护的世界发挥着重大作用。<br />
<br />
我们致力于人权的一个动力是我们对自己的道德价值观坚信不移；另一个动力是我们知道，当其他地区的人民冲破黑暗，砸开枷锁，赢得我们所享有并珍视的机会和权利时，我们自己的安全、繁荣和进步也将得到巩固。<br />
<br />
本着这一宗旨，我谨此向美国国会呈交国务院《2008年度各国人权报告》(Country Reports on Human Rights Practices for 2008)。<br />
<br />
国务卿<br />
<br />
希拉里・罗德姆・克林顿(Hillary Rodham Clinton)<br />
