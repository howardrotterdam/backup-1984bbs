美国承认西藏自治区和其他省份的藏族自治州、自治县及自治城镇为中华人民共和国的一部分。西藏自治区的藏族人口约为280万，西藏自治区以外地区的藏族人口约为290万。政府严厉控制有关西藏自治区及西藏自治区以外藏族地区的消息，对进入这些地区也严加限制，因此难以准确判断那里践踏人权的程度。3月14 日藏族居住区爆发大规模骚乱后，控制手段进一步加剧。<br />
<br />
<br />
<br />
本年度，政府在中国藏族居住区的人权纪录急剧恶化。当局继续严重践踏人权，包括采取酷刑、任意逮捕和未经法律程序进行拘留以及软禁。春季西藏高原爆发抗议示威后，官方明显加强了对言论、宗教、结社及迁徙等自由的限制。保护和发展西藏独特的宗教、文化和语言传统的问题仍然令人关注。<br />
<br />
<br />
<br />
3月10日，一些寺庙的僧侣和尼姑在拉萨和其他藏族社区举行和平抗议活动，以纪念1959年的西藏起义。在随后的几日内，非宗教民众也加入了示威行列。为了阻止游行活动，当地警察动用了催泪瓦斯，采取逮捕行动，还对当地寺庙及民宅进行搜查。3月14日和15日，公安人员武力逮捕一些包括僧侣在内的示威人员后，拉萨发生骚乱。一些示威者对汉族和回族居民采取暴力，在某些情况下出现致命事件。示威者砸毁非藏族人经营的店铺和政府大楼并进行盗窃。示威活动迅速蔓延到西藏自治区和青海、甘肃及四川等省的其他藏族社区。示威者对僧侣和尼姑表示声援，同时对宗教信仰等基本权利受到限制以及经济和教育机会不平等表达不满。政府因此向藏族地区派出大量人民武装警察，对示威进行暴力镇压，导致人员死亡。人民武装警察还进行任意搜查和逮捕，严格限制西藏人和外国人的行动。本年度下半期，西藏自治区及四川、甘肃和青海等藏族地区仍有示威活动发生，有时演变成暴力。<br />
