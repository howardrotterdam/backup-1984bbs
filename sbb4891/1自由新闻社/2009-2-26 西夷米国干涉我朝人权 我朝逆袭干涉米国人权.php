在外交上诚然是有实用主义的位置。中国政府面对其严重违反人权的现象也许会不理会美国的关切以及会叫我们不要干涉他们的“内政”。然而，我们只要看一看最近的历史上夏兰斯基和索尔仁尼琴的例子便可知道如果我们相信在公共场合下勇敢地为自由以及消除压迫呐喊的重要性，我们就会对那些遭囚禁的政治犯们带来多么大的希望。在这层意义上，这种做法有同样大的实用主义价值。<br />
