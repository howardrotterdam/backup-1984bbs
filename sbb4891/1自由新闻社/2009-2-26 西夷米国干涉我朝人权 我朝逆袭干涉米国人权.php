香港<br />
<br />
<br />
<br />
香港是中华人民共和国的一个特别行政区(Special Administrative Region, SAR) ，人口约为700万。1984年中英关于香港问题的联合声明(The 1984 Sino-British Joint Declaration on the Question of Hong Kong)以及香港特别行政区宪法，即香港特别行政区基本法(简称《基本法》)(Basic Law of the SAR)明文规定，香港除防务和外交事务外享有高度自治。9月，举行总体上自由和公正的选举，选出由地区席位和功能界别席位两部份组成的第四届立法会(Fourth Term Legislative Council, LegCo)。文官当局总体上对安全部队保持有效的控制。<br />
