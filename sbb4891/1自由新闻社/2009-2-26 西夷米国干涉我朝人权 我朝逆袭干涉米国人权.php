中国 <br />
<br />
<br />
<br />
约13亿人口的中华人民共和国是一个专制国家，中国共产党拥有宪法赋予的至高无上权力。政府、警察和军队最高层的几乎所有职务都由共产党员担任。最高权力属于由25人组成的中共政治局及其9人常务委员会。胡锦涛身兼中共总书记、国家主席和中央军事委员会主席三个权力最大的职务。文职政府总体上保持着对保安力量的有效控制。<br />
<br />
<br />
<br />
政府的人权记录仍然恶劣，在某些领域出现恶化。在本年度内，政府加剧了对藏族地区和新疆维吾尔自治区少数民族的严厉文化和宗教压制，增加了对异议人士和请愿者的拘押和骚扰，并继续严格控制言论自由和因特网。围绕奥运会和西藏骚乱等举世瞩目的事件，侵权活动达到高峰。同过去一样，公民无权更换政府。地方和国际非政府组织继续受到严格审查和限制。其他严重的侵权行为包括：法外杀人、对囚犯施行酷刑、逼供以及采用强迫劳动，包括使用监狱劳工。工人不能让独立工会作为自己在工作场所的代表，法律不保护工人罢工的权利。<br />
<br />
<br />
<br />
政府继续监视、骚扰、拘押、逮捕和监禁新闻工作者、作家、活动人士和辩护律师以及他们的家属，而其中很多人只是为了依法行使自己的权利。缺乏正当的法律程序和对律师的种种限制禁锢了法治发展，对那些被根据与国际标准相差甚远的法律程序判刑或处决的被告人具有严重后果。党和政府对法庭和法官实施严格的政治控制，实行不公开审判并采用行政拘留。个人和团体，特别是被政府视为政治敏感的个人和团体，在集会自由、宗教活动自由和旅行自由方面继续受到严格限制。政府继续推行强制性的限制生育政策，有时导致强迫堕胎和绝育。政府未能充分保护难民，拘留和强制性遣返北韩人的问题依然存在。影响人权的严重社会问题包括：腐败盛行、人口贩运以及对妇女、少数民族和残疾人的歧视。<br />
