&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp;&nbsp; &nbsp; 昨晚睡的较早，预备闹铃8点，因为今日9点要与同仁一起“穿越地震带”，这个策划做了一个多月。闹铃响了，翻身起床，刚穿上裤子，手机骤然响起，电话字头 “189”的一个陌生电话那头，一个陌生声音似晴天霹雳：“北川宣传部的冯翔自杀，在绵阳现代花园B区，估计不行了……120已经来了……”<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;我马上拨打冯翔电话，语音提示：无法接通。马上告知中新社驻绵阳记者站站长肖青，她无限吃惊。很快，她回电话：“北川宣传部副部长王建正在往他家赶……”<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;10分钟后，我电话再响，那个陌生声音哽咽告知：“他走了，120已经撤离……”<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;进一步确认，冯翔是凌晨7点告别的人生。那个陌生声音是冯翔孪生哥哥……<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; 冯翔是我在北川走的最近的一个哥儿们。彼此兄弟相称。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; 认识他，是因为地震后频繁采访，他的官方身份是北川宣传部副部长。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; 之所以走的很近，更因为志向相投――他原是绵阳日报驻北川记者站站长，大家都吃记者饭。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; 地震以后，他最爱的儿子冯瀚墨被巨大的山体所掩埋，至今不知所踪。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; 他一直很悲伤。开导并劝过他。不过，他是文人，很能调控自己情绪。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;去年10月，北川农办主任董玉飞自杀后，我和冯翔就此事在QQ上聊天。他说――<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; “董玉飞自杀是必然。北川很多干部都有自杀倾向……”<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;“没准哪一天，我也会自杀……”<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; 就这话题，我们QQ聊天了一晚上。最后，他连续发了几个开心的QQ表情说：“党哥，放心！我不会自杀，我会很好活着，看看北川的未来！”<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;北川志愿者张华强说：“前几天我还和他QQ上聊，他说在北京。从聊天记录看，他心情应该不错，绝对没有想走绝路的意思……”<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; 中新社驻绵阳记者站站长肖青给我说：“清明节，我和冯翔一起在北川祭奠。回来路上，我陪他走了很长一段……我还问他：冯翔，现在应该走出来了吧，冯翔当时笑声很爽朗……”<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;谁料想，4月20日，汶川大地震周年纪念前夕，他竟如此决断！<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; 冯翔的网名是“残月苍山”。获悉惊人消息后，我立即进入他的空间。空间置顶：2009年04月20日 00:53发表《很多假如》。再看他的心情签名，他最近几天的心路竟是如此起伏跌宕――<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;2009-04-18 QQ签名：装扮了空间，换成了郁郁的黑色……<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; 2009-04-17 QQ签名：受中宣部政研室领导朋友的盛情款待，感激并不安中……晚中央电视台刘主任接风……<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; 2009-04-16 QQ签名：一切都不永恒，但死亡例外……（见426集体名单有感）<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; 2009-04-16 QQ签名人在京城，心在帐篷……<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; 伴随他刻意的空间背景音乐《风居住的街道》的悲怆，细读，这竟是他留给亲人的最后遗言――<br />
《很多假如》<br />
&nbsp; &nbsp; 假如，某一天，我死了，哥哥，请您担当起照顾父母的重任，我来到这个世间，本就是来体会苦难，承受苦难的。要不，我们怎么能以孪生兄弟的面目出现。<br />
&nbsp; &nbsp; 假如，某一天，我死了，妻子，请你不要悲伤，抑郁，是我这三十年来，最亲近的朋友，抑郁带走了我，也就带走了所有的悲伤。<br />
&nbsp; &nbsp; 假如，某一天，我死了，爸爸，请您不要哭泣，我真的活得太难了，人生为什么总是充满苦难，充满艰辛，充满离愁…………<br />
&nbsp; &nbsp; 假如，某一天，我死了，妈妈，请您不要难过，短短三十年，我体会到了您对我的爱，对我无微不至的关照，但是，我实在觉得活着太痛苦了，请您让我休息吧，真的，让我好好休息休息……<br />
&nbsp; &nbsp; 假如，某一天，我死了，儿子，那是我最幸福的事，我会让你妈妈，把我的骨灰，撒在曲山小学的皂角树下，爸爸将永远地陪着你，不弃不离……儿子，你离开了，爸爸没有了未来， 没有了希望，没有了憧憬，与您相聚，是爸爸最大的快乐……<br />
&nbsp; &nbsp; 假如，某一天，我死了，亲爱的朋友，请你们不要忧郁，我的离去，让很多人快乐，让很多人舒服，我的存在，是他们的恐惧，是他们的对手，一个对手的离去，对于他们，是多么值得庆贺的事情啊！<br />
&nbsp; &nbsp; 假如，某一天，我死了，我的儿子，我还是要提到你，我们将不离不弃，永远在一起……相信一个父亲，对你最深，最深的爱……<br />
&nbsp; &nbsp; 假如，某一天，我死了，亲爱的网友们，感谢你们一直以来的关心、爱护，我相信，假如，我在天堂，我能够进入天堂，我会许你们，一个没有痛苦的来生，谢谢你们……谢谢……<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;地震发生时，北川县委宣传部副部长冯翔一家都在县城，但妻儿各在一方。要照顾身边的母亲，又万分担心妻儿，他当时的心情，如果不是一个羌族汉子的坚强，他一定会疯狂掉。地震发生后，他仅仅在14－ 15日，到成都市新都区人民医院临时救护站照料尾椎骨折的妻子外，5月16日，带着失去儿子悲痛的冯翔，带领成都两家公司组织捐赠的10箱药品和整整一卡车饮用水、方便食品就回到了北川。 回到北川，他一直战斗在抗震救灾前线。<br />
&nbsp; &nbsp;&nbsp; &nbsp;5月17日，受县指挥部委派，他为部队官兵带路，到坝底乡查看灾情。当时灾情非常严重，道路全部中断，四面山中，滑坡比比皆是。他们冒着余震，翻山越岭，整整徒步走了两天， 18日下午才到达坝底乡……<br />
&nbsp; &nbsp;&nbsp; &nbsp;5月18日至20日，他在坝底乡协助乡党委、政府收集灾情材料。21日，他带着坝底乡灾情资料，徒步行走两天，翻越6座大山，才艰难回到县指挥部，将灾情及时上报……（详见发表于2008年07月07日的博文《悲怆北川（第二季・七）鲜血和悲痛 成为他内心永远厚重》本文链接<a href="http://user.qzone.qq.com/6956208/blog/1215362881" target="_blank">http://user.qzone.qq.com/6956208/blog/1215362881</a>）<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;按照组织部要求，冯翔也写了自己“5・12纪实”。他的“纪实”粗看就是一个“汇报”，基本没有一点心理、心情方面的描写和其他文学修辞。<br />
&nbsp; &nbsp;&nbsp;&nbsp;他不是不会煽情和细节描写。<br />
&nbsp; &nbsp;&nbsp;&nbsp;要知道，担任北川宣传部副部长之前，他是绵阳日报驻北川记者站站长！<br />
&nbsp; &nbsp;&nbsp;&nbsp;“5・12”那段时间，采访他很难。他不愿意多谈当时情况，更不愿意对记者最感兴趣的 “细节”多说。当时的我，只能从他的“纪实”和5月16日起至今一直不愿休息玩命工作读出：发生在北川那惨烈一幕，鲜血和悲痛，装满这个羌族汉子内心成为一种永远厚重，玩命工作是他唯一减压和发泄方式……<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;他那样的厚重内心，注定心理有严重问题。因此，我经常去北川采访时，约他喝酒谈天，试图解开他心理郁结……去年冬天，我还专门给他送去寒衣、棉被……<br />
