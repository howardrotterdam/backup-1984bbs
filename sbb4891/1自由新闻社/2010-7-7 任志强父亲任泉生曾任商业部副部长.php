													<h2>任志强父亲任泉生曾任商业部副部长</h2>
						
						
																											<div id="postmessage_725854" class="t_msgfont">任志强家世曝光 父亲任泉生曾任商业部副部长<br />
2010年07月06日 21:44东方企业家<br />
封面文章任志强：傲慢与偏见<br />
<br />
文：李志刚 <br />
<br />
大连住宅产业化和房地产业高峰论坛的主席台上，任志强正准备发表演讲。一位穿运动装、戴眼镜的年轻小伙子，大叫着“闭嘴吧”，将臭烘烘的运动鞋扔向了主席台。在短暂的混乱后，任志强自嘲受到了总统待遇，继续神态自若地演讲。<br />
<br />
此前，主办方播放了《蜗居》的一点片段。《蜗居》是去年热播的电视剧，引发了广泛而尖锐的争论，它描述了城市白领身为“房奴”的困境，以及年轻女性投怀送抱权贵的生活。在发表演讲前，任志强调侃说，贪官宋思明开的车比蜗居还贵，这直接激怒了“扔鞋男”。<br />
<br />
“扔鞋男”25岁，是大连当地人，与父母共同居住，家中积蓄不够支付一套房子的首付。先后两位女友因房子问题而离开他。他正是当下陷入高房价困境的中国年轻人的典型代表。他的扔鞋举动，被反应迅速的媒体放到网上，赢得很多网友的叫好。这正折射了贫富尖锐对立的中国房市如火药桶一般，“房奴”或者被无房困扰的网民们怨气冲天。<br />
<br />
在北京、上海这样的城市，倾两代人三个家庭的所有，也许还不够一套房子的首付。而任志强屡出“房子是给富人盖的”、“买不起房就该回农村”的惊人之言，被神经因房价变得敏感的公众视作高房价的鼓吹手、“吸血鬼”地产商的代表。凤凰卫视[1.82 0.00%]执行台长刘春在与任志强首次聚会前，曾在微博里写：“明天我准备和任老板刺刀见红，给他普及一点中国贫民的知识”。<br />
<br />
微妙的是，从2005年最想揍的人之一到2009年真男人，任志强在公众中的形象正在经历转变，越来越多的人将他视作《皇帝的新衣》里那个说真话的小孩。他指出了房市的最大获利者正是政府。财经作家吴晓波则在他的专栏里写道：“任志强也许是地产界读书最多、最清醒的人，也是最具有思考能力的人之一，可他又是最被批判、最被妖魔化的人。”<br />
<br />
本刊试图在这种背景下，进入任志强的世界，对他进行一些了解……<br />
<br />
59岁的任志强留给公众的印象相当固定：多年保持不变的发型、宽脑门上紧锁眉头刻下的深纹、绷紧的腮帮子、几乎瞪出眼眶的眼珠。他似乎无时无刻都是这副严厉、咄咄逼人的模样。两年前曾采访过任志强的财经作家苏小和告诉我：“当时他在等我，假装在看书。开始爱理不理，后来很客气。”<br />
<br />
在任志强的办公室里，我采访过他两次。隔着宽大的办公桌，他往后斜倚着靠在椅背上，脸上没有表情。当他想强调他想表达的意思时，眼睛会直直地瞪着你。第一次采访了一个半小时，任志强总共有八次打断或反驳我的问题：“你的概念是错的”、“你这个问题太无知了”、“你们将问题想偏了”……第二次采访他，他依旧面无表情。我说：“任总，上次在这里采访过你，还记得吧？”他斜靠在椅子上，眼睛向上挑了挑说：“我知道你。”<br />
<br />
阎阳生是任志强在38军的老战友，曾任全国工商联执委和《中国工商》杂志总编辑。他曾带一年轻女记者去任志强办公室采访。任志强面无表情，把女记者吓得直打哆嗦。阎阳生怪他把小姑娘吓成什么样了，任志强反而哈哈大笑起来，一笑那女孩更害怕。“任志强是一个很有气场的人”。<br />
<br />
前华远集团副总裁、与任志强共事二十年的齐跃说，很多人不愿意去任志强的办公室，哪怕是跟他非常熟的人。在外面他还会说笑，一回到办公室那个环境，就变成了瞪着眼，没什么笑容、面无表情。<br />
<br />
任志强曾专门写过一篇文章，解释自己为何在办公室总是绷着脸。“一到工作环境中，我脑海里就出现了很多跟工作有关的事情，自然而然就进入这个情景，面部表情就会相对严肃了。离开办公室后，我就放下这些东西了，也会笑。”<br />
<br />
任志强是北京市西城区主管的国有企业华远集团总裁、华远地产[6.47 7.12%]董事长。从规模上讲，在全国房地产公司里，华远地产不算大，去年营收11。60亿元。从个人财富上讲，任志强是替国有企业打工的，远谈不上房地产行业里赚钱最多的人。但是，任志强针对政府对房地产业的宏观调控多次撰写万言书，以尖锐的言论著称，他是地产界抨击政策声音最响亮的人，也是地产界最具争议性的公众人物。<br />
<br />
任志强对中国房地产政策研究有着非常的嗜好。2003年，他牵头与几家企业联合成立了研究机构，专门研究房地产的数据。“从房地产行业来说，我敢说，中国最系统的房地产数据研究机构是我们的。”任志强表示。他的地产界朋友、生意伙伴、SOHO中国[4.60 -0.86%]董事长潘石屹以前与人PK，会发短信求助任志强，向他索要数据。曾经多次和任志强一起参加论坛的中坤集团董事长黄怒波说：“任志强确实比很多专家比如易宪容、谢国忠等要强，那些专家没有数据支持，而任志强有。”任志强是住房和城乡建设部的顾问，住建部、地方政府也时常到任志强这里索要数据，咨询意见。任志强自称是在做“开启官智”的事，从对政府23号文件的质疑到参与18号文件的起草，他在中国房地产业的“国策顾问”这一条路上越走越深。<br />
<br />
华远地产人力行政总监李春晖透露，任志强要求华远高管人员看《新闻联播》。任志强未必认为国家说的百分之百都是对的，但他认为《新闻联播》一定是国家最重要的一个新闻导向，它传递的是政府政策导向，你要了解它，你要知道政府现在在支持什么，它要做什么东西……<br />
<br />
任志强对政治、政策的热情，也许与他本人的出身、经历有关。从上山插队到下海创业，他几次命运的巨大改变，都受到国家政策的影响。 <br />
<br />
1951年3月8日，任志强出生于一个典型的红色高干家庭。任志强的父亲叫任泉生，1918年7月生于山东省掖县，2007年8月在北京过世，当时新华网发了通稿。据新华网介绍，任泉生1937年参加革命，曾是新四军干部，解放前担任过中原局税务局局长，解放后担任过商业部副部长；母亲在文革后期主管北京副食、烟酒等生活必需品的供应。<br />
<br />
任志强初中就读于北京35中学，这是当时干部子弟比较集中的中学。据他的同学赵晓航回忆，任志强当时的家位于西城区平房区，是一个独立院子，红漆大门，车能开进去。平房从外表上看不出什么，但里面装修是西式的，铺着木地板，安有暖气。<br />
<br />
任志强说，他家当年的生活其实相当困窘。小时候去照全家福，身上经常是破衣服、露脚趾的鞋。平时就用哥哥姐姐的衣服接接袖子裤腿，打上补丁继续穿。他说当时整个国家都在挨饿，没有一个家庭不挨饿，只是程度不同。他每顿都吃不饱，早晨稀粥加一片窝头片，中午是红薯面的窝窝头。<br />
<br />
回忆起母亲，任志强的表情柔和了一些，语气平缓。“我的母亲是一位马列主义老太太。”他身体前倾靠在桌子，右手肘支在桌面上，右手握着拳头抵着头。时不时陷入深思中。他对母亲最深刻的印象是，当红卫兵冲到家里来时，母亲手握着刀拦在门口，保护背后的儿女。他还记得当时缺粮，家里一碗饭要用筷子分成两半，每人半碗。任志强用右手食指比作筷子，斜着虚划了一道线：“我母亲下筷子是斜着划的，这样表面上看是两半，实际上我的一半饭量比她多。”<br />
<br />
我问任志强是否从小缺少母爱，任志强矢口否认这一点：“我没有说，是潘石屹说的。我的父母他们平常为了工作把我们放在保姆家里，小时候住校，放在阿姨家里。潘石屹就说我缺少母爱，这是两回事。我认为这恰恰是一种爱。他们爱更多的人。”<br />
<br />
但我找出了去年10月11日采访任志强的录音，任志强是这样说的：“我很悲哀，我从小没有父爱母爱，都是别人在带我。潘石屹认为交警卫员带是自豪的事，我觉得是悲哀，没有母爱，父母在战场上，只能交给警卫。从我的角度，我最需要的是母爱，从小部队今天走到这，明天走到那，跟着走。刚开始解放，到处跟着跑。这是两个不同的概念，从他的角度当然羡慕我。1988年，我家惨得很，那些破东西扣了我父母好几年工资，我宁愿扔了再买，但就是不行，很多年轻人就是不了解这些事。”<br />
<br />
文化大革命和知青上山下乡运动冲击了这个家庭，全家人天南地北各居一方。<br />
<br />
任志强母亲战争年代就有很严重的关节炎，在文革时期去了辽宁盘锦干校种水稻，住棚子，站在冷水中插秧，这加重了她的关节炎。这样，家也散了，任志强和他的兄弟姐妹都是独立或半独立生活状态。在那个特殊的年代，他被卷入了时代的洪流，他与同学们一起抄过“成分不好”的人家，押送过人回原籍。“我们就认为毛主席说的都是对的，我们就该去做。”他说，“我们从小就受这样的教育，包括大炼钢铁，在学校都是大炼钢铁。现在看来是胡闹，但当时也会去做这种事。那时候我们到处捡钉子，捡破铜烂铁。那时候学生的一片热情就是要解放全世界，不让大家受穷。所以毛主席说打到哪我们就打到哪，包括毛主席说让我们下乡。当时这是普遍意识。”<br />
<br />
对于命运巨变，任志强的父母和他本人，都认为天生该如此。1968年末，任志强作为知青去了“革命圣地”延安。他曾在博文里写道“他们(父母)并不知道将子女送出家门后的未来，甚至不知道自己的命运如何，但他们知道只要党中央、毛主席发出了指令，他们就一定会奋不顾身地冲锋在前，这已成为一种深刻在头脑中、融入血液里的信念和习惯。”<br />
<br />
去延安的基本是17岁左右的学生。最小的是14岁的毕京京。毕京京记得，从北京出发总共花费五天才抵达插队的郭庄。当地支书说，延安这地头是革命圣地，旱涝保收，如果旱了就沟里收，如果涝了就坡上收，就是靠天吃饭。当时当地亩产也就四十斤左右。一个大队的全部财产就是一个碾子、一辆价值80块钱的驴车、几头牛、几十只羊。延安1971年的GDP仅仅相当于1946年的GDP。<br />
<br />
今年5月6日，我去了延安市宝塔区郭庄村，离延安城区约40公里，现在已全是水泥公路，坐车一小时就到了。我找到了任志强曾居住的窑洞。在长着荒草的低矮山坡上，有两个破败的窑洞。约2米高，上面是白纸糊的窗子，下面是一米多高的木板门，个高的人进去需要低着头。我把照片拿给任志强看，他说，这是后来翻修过的。当时的窑洞比这还破。<br />
<br />
在郭庄，知青需要学做农活，什么都干，种小麦、小米、荞麦、玉米、红薯等，第一年每个人只分得七斤小麦。在同去的知青眼里，这些年任志强变化不大，他当时就很直爽、很实在，干活从不投机取巧，像个小老虎似的。一次割麦子的时候，镰刀把任志强的脚给划了。有的农妇就叫了起来：“哎呀，流血了。”任志强拿起一把土抹在伤口上，连说：“没事、没事。”“他特别粗犷，当地人都说他是一个干农活的好把式。”毕京京说。<br />
<br />
当地没有水，喝的是黄泥汤子，搁到缸里用白矾沉淀。几天时间就需要洗一次缸，要不然沙土就积了半缸。盛水的时候不能搅动，一搅沙子都起来了。“经常缸里出蛤蟆，很正常的。”任志强说。如同他的父母一样，他认为吃苦理所当然。</div>

							
							
							
							
