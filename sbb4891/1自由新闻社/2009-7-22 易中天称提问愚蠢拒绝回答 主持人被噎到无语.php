																											<div id="postmessage_173584" class="t_msgfont">晨报讯 （记者冯遐）易中天前晚作做客北京卫视科教频道的《非常网络》，在面对主持人所谓的“愚蠢”提问时，易中天不再配合，甚至拒绝回答问题，现场气氛僵到极点，两位主持人几度被噎到无语，甚至眼泛泪光。很多网友认为易中天对主持人和媒体的冷嘲热讽显得其偏执狭隘、狂妄自大；而科教节目中心主任徐滔则表示，“我们应该认识到自身的差距和不足，主持人不懂就是不懂。”<br />
<br />
　　“砸场”事件的导火索是主持人问易中天有没有想到《品三国》图书火爆等话题，易中天以“不要以为什么事儿都是策划的，媒体很弱智，总喜欢问动机”作答；此外，在被问到“与余秋雨的网络战是不是为了保持名人热度”等问题时，易中天干脆说，“我拒绝回答愚蠢的问题。”易中天还多次以讥讽的口吻问主持人还要继续愚蠢下去吗？嘲讽应该开个主持人培训班；质问主持人怎么就是不明白自己的意思等等。主持人林白和姜华几度被噎到无语，特写镜头中甚至能看到他们面露难色、眼泛泪光。<br />
<br />
　　诸多网友就此事件昨日发表评论，大多数认为，虽然主持人的提问水准一般，但易中天的表现算不上是一个有修养和懂得尊重人的学者。从他的言谈可以看出，他对所有问题都怀着一种戒备和防御的态度，他已经习惯了认为所有人问的话里面都存在着陷阱。<br />
<br />
　　不过，科教节目中心主任徐滔在接受记者采访时认为，“我觉得很正常，易中天老师就是一个很有个性的人。为什么只能是主持人给别人上课呢？主持人也得像别人学习，跟易中天老师这样的嘉宾做节目就是吸氧的过程，只有大吸才能大放。主持人不懂就是不懂，我们只有认识到这种差距和不足，并把它放大，才能有进步。观众和网友之所以讨论这件事，是因为他们平时很少看到这种最真实状况的展现，为了追求节目的完美，像这样的碰撞甚至说尴尬可能会被删掉，而这次我们愿意把最真实的一面呈现出来，也是我们追求的一种态度。两位主持人录完节目后也跟我聊了几句，他们说气氛很热烈，学到了很多东西。”<br />
