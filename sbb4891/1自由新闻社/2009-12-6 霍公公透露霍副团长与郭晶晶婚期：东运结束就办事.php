2009年12月06日09:32<br />
<br />
<img src="http://www.ce.cn/xwzx/gnsz/gdxw/200805/07/W020080507334342228235.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　本报讯　从雅典奥运会夺冠后访港开始,郭晶晶就拨动了香港媒体的神经,原因很简单,因为男主角是霍启刚。从相识到相恋,再到此刻传出即将结婚的消息,“晶刚恋”一直揪着人们的视觉神经。在香港举行的东亚运动会,郭晶晶没有不来的道理,作为接待酒店昨天也表示将给霍家“准儿媳”特殊待遇,郭晶晶到港后将从普通客房升级入住贵宾房。<br />
<br />
　　郭晶晶住宿升格<br />
<br />
　　据了解,本次参加东亚运的中国跳水队将入住香港龙堡国际酒店。昨天这家酒店表示将给跳水女皇特殊待遇,把郭晶晶的接待标准从普通客房升格为商务房,而且这类房间在每个楼层只有一间,用来安排贵宾入住,加之房间距离电梯很近,方便贵宾出入,也有利于酒店安排安保工作。<br />
<br />
　　据了解,龙堡国际酒店多年前就接待过国家跳水队入住,不过当时队中的大牌是伏明霞。如今郭晶晶的名气已经如日中天,作为接待酒店本次给霍家“准儿媳”升级比赛住房也在情理之中。<br />
<br />
　　霍公子荣升副团<br />
<br />
　　据了解,中国跳水队将于9日抵港,到时前往迎接郭晶晶的霍启刚不再只是“霍公子”了,女友在港比赛期间的住房升格了,霍启刚也升官了,将首次以东亚运动会香港代表团副团长的身份亮相。<br />
<br />
　　在谈到担任香港代表团副团长的感受时,霍启刚显得非常激动,“让我当这个副团长,可能是因为我参加运动会的机会比较多。不过这是第一次在香港举办东亚运动会,也是第一次以主人的身份迎接参赛选手,当然有点紧张,所以每天都在忙。”而对于是否会去看跳水比赛时,霍启刚也毫不掩饰地给予了肯定的回答。“每场比赛应该都会去看吧,我们香港也有运动员参赛。”<br />
<br />
　　对于郭晶晶的参赛,作为香港奥委会主席的“未来公公”霍震霆其实出力不少,他坦言,郭晶晶是一名优秀的运动员,香港市民希望现场看到她的精彩表演。霍震霆在媒体逼问儿子与郭晶晶婚期的时候,首次透出了这样的口风:“过了东亚运动会就办。”<br />
