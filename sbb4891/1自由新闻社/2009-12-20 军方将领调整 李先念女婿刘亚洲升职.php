																											<div id="postmessage_383763" class="t_msgfont">DWNEWS.COM-- 2009-12-18 13:07:11.60 --多维新闻网<br />
<br />
法国广播电台香港特约记者 郑汉良／根据中国大陆传媒报道，中国军方高级将领在年底展开一轮新的人事调整，涉及大军区、军兵种以及总部层面。其中前国家主席李先念的女婿刘亚洲中将，传将调升为国防大学政协职务，兰州军区政治部副主任王建民少将将出任新疆军区政委。<br />
<br />
消息还透露，前中央军委副主席张震之子、成都军区政委张海阳将转任第二炮兵政委，国防大学政委童世平海军中将调任总政治部副主任，总政主任助理杜金才升任总政副主任，总政副主任刘永治和孙忠同到龄退役，而新疆军区政委田修思则接任成都军区政委。<br />
<br />
刘亚洲曾在1992年4月以作家身份访问台湾，开创了大陆现役军官访问台湾的先河。本人同时也是军事评论家的刘亚洲，曾任北京军区空军政治部主任、成都军区空军政委。2003年12月任解放军空军副政委，现为中将军衔。有评论认为他是中共军方内具有民主思想和要求政治体制改革的人物。他与妻子曾在美国生活，在此期间曾以笔会会员的身份参加独立中文笔会的一些活动。<br />
<br />
刘亚洲在八十年代初以一批描写新军事变革的战争作品著称，如《恶魔导演的战争》、《那就是马尔维纳斯》、《攻击攻击再攻击》等，为刚刚走上改革之路的中国军队开启了通向世界的窗口，引发强烈反响。<br />
<br />
另方面，根据大军区正职的任职年限，同样在1944年出生的副总参谋长葛振峰、总政治部副主任刘永治和孙忠同已届65岁大军区正职任职年限。而资料显示，二炮政委彭小枫为1945年2月出生。<br />
<br />
军情观察人士指出，如果此番将领调整属实，那么这其中不乏一些军中元老后代得以提拔攫升。<br />
<br />
如转任第二炮兵政委的张海阳为中央军委前副主席张震之子；而调升国防大学政委的刘亚洲更为原国家主席李先念的女婿。<br />
<br />
从今次获得提升将领的年龄及学历看，可以透出中国军队未来选拔高级干部更加注重年轻化及高学历。如刘亚洲、田修思均为「50后」。而田修思为研究生学历，童世平为国防大学研究系将官班毕业，刘亚洲为武汉大学英文系毕业。<br />
<br />
而军情人士也指出，从此番将领调整看，可以窥出中国军队将领调整的新思路。包括打破条块限制，实现交叉任职，跨领域、跨战区等特点。</div>

							
							
							
							
