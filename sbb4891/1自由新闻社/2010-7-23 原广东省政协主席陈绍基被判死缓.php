<br />
2010年07月23日11:41<br />
<br />
<img src="http://img.hexun.com/2010-07-23/124350660.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
资料图片：图为陈绍基<br />
<br />
重庆市第一中级人民法院23日对广东省政协原主席陈绍基受贿案作出一审判决,认定陈绍基犯受贿罪,判处死刑,缓期二年执行,剥夺政治权利终身,没收个人全部财产。<br />
<br />
　　重庆市第一中级人民法院经审理查明,1992年2月至2009年4月,被告人陈绍基利用担任广东省公安厅厅长、中共广东省委常委、广东省政法委书记、广东省委副书记、广东省政协主席的职务便利,为他人谋取利益,单独或者伙同其子陈子翊、情妇李泳(均另案处理)索取及收受他人给予的财物,共计折合人民币2959.5万余元。<br />
<br />
　　被告人陈绍基归案后能够主动交代办案机关尚未掌握的大部分受贿犯罪事实。案发后,赃款、赃物已全部追缴。<br />
<br />
　　重庆市第一中级人民法院认为,陈绍基身为国家工作人员,利用职务便利,为他人谋取利益,索取和非法收受他人给予的财物,其行为构成受贿罪,且受贿数额特别巨大,情节特别严重。鉴于陈绍基因涉嫌受贿被审查后,主动交代了办案机关尚未掌握的大部分受贿事实,认罪、悔罪,且案发后赃款、赃物已全部追缴,对其判处死刑,可不立即执行。重庆市第一中级人民法院遂依法作出上述判决。<br />
<br />
