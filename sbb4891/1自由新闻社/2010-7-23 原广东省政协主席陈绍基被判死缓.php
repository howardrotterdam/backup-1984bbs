																											<div id="postmessage_756505" class="t_msgfont"><img src="https://yopic.us/images/2010072323.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
7月23日，重庆市第一中级法院对广东省政协原主席陈绍基受贿案作出一审判决。法院认定陈绍基犯受贿罪，判处死刑，缓期二年执行，剥夺政治权利终身，没收个人全部财产。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　经法院审理查德明陈绍基的犯罪事实，与起诉书所指控基本一致。其被指控于1992年2月至2009年4月间，利用担任广东省公安厅厅长、中共广东省委常委、广东省政法委书记、广东省委副书记、广东省政协主席的职务便利，为他人谋取利益，单独或者伙同其子陈子翊、情妇李泳（均另案处理）索取及收受他 人给予的财物，共计折合人民币2959.5万余元。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　《外参》杂志报导，陈绍基接受中纪委调查后，原广东省公安厅班子成员普遍接受组织谈话。而2010年以来，广东公安系统则不时曝出官员负面消息。汕尾、佛山、茂名等地公安很多被谈话。<br />
&nbsp; &nbsp; <br />
<br />
许俊民千里送燕窝<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 汕尾市(地级市)委常委、市公安局局长许俊民5月上旬被抓。许俊民出事传言，最早在去年4月。当月中旬，广东省原政协主席、公安厅长陈绍基被查。许俊民系陈绍基的亲信，陈一下台普宁人就会联想起许。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 广东政法系统多位官员分析认为，许俊民与陈绍基关系密切，其落马不过是时间问题。财新网报导，广东政法界广为流传的故事是：陈绍基走上权力顶峰，即其担任广东省委副书记、政法委书记，省公安厅厅长、党委书记后，时任汕头市公安局副局长的许俊民多次约请陈绍基吃饭。后者以工作忙碌为由婉拒。此后许俊民携带做好的燕窝等炖品，从千里之外的粤东送往广州陈绍基家中。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 外参编辑注意到，网友有言：许俊民早该枪毙了！早在八几年他靠陈绍基，胡作非为，横行普宁，祸害伯姓，民愤极大。无耐有陈绍基庇护，每出一次事就挪一个地方，升一次官。不查处许俊民，民愤难平。许俊民早在普宁城西派出所任所长时，他偷三斤多该所缴获的黄金被发现时，有句很雷人的话：“黄金水水汁汁的对象，不到斤两是正常的。&quot;<br />
&nbsp; &nbsp; <br />
<br />
“许仔”从民警爬上来<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 许俊民今年56岁，广东普宁占陇华林村人，民间叫他“许仔”。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 1976年，许俊民从北京86537部队退伍后，任过普宁水电局保卫组长、韶关市红工矿务局救护队副队长、普宁市农业局保卫组长。1984年正式进入公安系统，历任普宁市公安局军埔派出所民警、占陇派出所副所长、城西派出所所长，1991年任普宁市公安局副局长，1999年后任揭阳市公安局十四科副科长、科长、副局长兼刑警支队长，1999年任汕头市公安局副局长。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 2005年12月，汕尾红海湾爆发警民冲突，该冲突至少造成2人死亡。官方通报说，警方开枪示警造成误死误伤。此后包括汕尾公安局长李敏、副局长吴声在内的多位政法系统官员受到行政处分。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 而事件发生一年后，许俊民由汕头公安局副局长调任汕尾市委常委、公安局局长，由此结束他长达15年的副职履历，成为地市公安局的一把手。此后，他在粤东潮汕地区被视作陈绍基的代理人。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 陈绍基接受中纪委调查后，原广东省公安厅班子成员普遍接受组织谈话。而2010年以来，广东公安系统则不时曝出官员负面消息。汕尾、佛山、茂名等地公安很多被谈话。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 2010年1月，汕头市公安局经济犯罪侦查支队支队长范志强接受组织调查。范于2009年曾获五一劳动奖章。知情人士表示，范与许俊民关系密切。<br />
&nbsp; &nbsp; <br />
<br />
佛山公安经侦队长吴光华被双规<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 20101月13日上午10点34分，佛山本地网网友“2414829”发帖称，“据可靠消息，市经侦支队某领导被纪委双规？有待进一步证实。”此帖一出，立即引来众多网友关注。<br />
&nbsp; &nbsp;&nbsp;&nbsp;吴光华长期担任佛山市公安局经侦支队领导，担任经侦支队政委多年。2008年底起，他才从政委转任经侦支队长，行政级别为副处级，到事发刚刚好一年。在担任经侦支队领导期间，他曾负责侦办南海“806”案、“健力宝案”、“顾雏军案”等一批经济大案要案，是佛山警界一名资深警官。<br />
&nbsp; &nbsp;&nbsp;&nbsp;　　2009年6月4日，包括李经纬在内的四名涉案人员被重新逮捕。知情人士介绍说：此案时隔多年后被旧事重提，亦与陈绍基供认收受当事人贿款有关。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　平时吴光华工作、为人怎么样？记者询问经侦多位民警，但大都是讳莫如深。但有人表示，他人还不错的，2008年12月4日，佛山尚晖苑“O牌车阻塞小区”，当年闹得很大，当年还是市公安局经侦支队政委的吴光华带领涉事警员到尚晖苑小区向小区业主和物管表达歉意。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　另外，记者了解到，他非常有想法，2008年曾在省公安厅的内部交流平台上发帖提建议，帖子受到省公安厅领导表扬。对于这个帖子，省公安厅领导作出批示，要求全省民警学习。<br />
&nbsp; &nbsp; <br />
<br />
杨光亮向陈绍基王华元行贿<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　在公安系统之外，广东茂名市委常委、常务副市长杨光亮落马显示广东官场仍在陈绍基案余波震荡之中。杨光亮于2009年10月16日到广州开会时，被中央纪委连人带车直接扣下。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 知情人士称，杨光亮通过茂名市前政法委书记陈沃培结识陈绍基。一网民在网上发帖称，杨光亮通过茂名市政法委书记陈沃培（已去世）认识了陈绍基。在茂名市政府2006年换届选举中，杨光亮为力争市长一职，向陈绍基和时任广东省纪委书记的王华元巨额行贿。不过，杨的职位在此次换届中并未变动。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 杨光亮还涉嫌卖官、受贿等多项严重违纪问题，但这些均有待纪委等部门调查确认。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　杨光亮是广东电白县人，1954年生。1974年3月起参加路线教育运动，后任电白县海坡大队党支部书记。1977年起在湛江农专读书，1980年6月起在电白县大榜公社工作，任过公社党委副书记。1983年起先后任电白县羊角区委副书记、区长，羊角区(镇)委书记。1987年起先后任电白县副县长，县委副书记。1993年起先后任电白县委副书记、县长。1995年7月起任电白县委书记、县长。1996年4月起任电白县委书记。1998年任茂名市政府副市长。2003年任茂名市委常委、茂名市常务副市长。<br />
&nbsp; &nbsp; <br />
<br />
有人举报杨光亮犯罪事实<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 外参编辑注意到，有人举报了杨光亮在任电白县县长、县委书记期间的违法犯罪事实：<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 一、官商勾结，私分贷款。杨光亮与广东中X集团有限公司董事长林X润（又名：林X彪）勾结，以电白县财政担保，向广东国际投资公司贷款10亿元，名为加大经济建设，实际是与林X润私分，杨光亮得到3亿元。电白县财政没有能力承担债务，只有扣克全县工作人员的工资偿还债务，全县工作人员只靠几百元过日子，甚至有时几个月没有发工资。全县干部、职工过着水深火热的生活。我们多次向广东省反映情况，由于杨光亮有陈绍基、王华元做保护伞，问题不了了之。电白县拿我们大家的工资为贪官杨光亮、奸商林X润还外债的问题何时才休？<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 二、非法征地获益2000多万元。1994年，杨光亮利用县政府的名义以搞开发区，建工厂为幌子，征用了彭村所属的全部2000多亩土地（中央电视台《今日说法》的《土地之争（下）――征地背后》播出日期：2004年4月20日，记者：孙静婧、李政林，编辑：王宝卿，主持人张绍刚，嘉宾：中国政法大学薛刚凌教授）。杨光亮指使他人注册及虚构20多家公司，炒作买卖所征得的部分土地，非法获益2000多万元。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 三、受贿。325国道电白段修建时，杨光亮受贿1000万元；电白县三角墟19层为何倾斜了50多厘米，成了烂尾楼，杨光亮受贿800万元。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 四、卖官。电白县的局长、书记明码标价80万元一位，镇长等副职50万元一位。原电白县教育局长陈建明就是买官的一员，所以才有了2000年轰动全球的电白县高考舞弊案。为什么一年之久不查陈建明行贿行为呢？究其原因：杨光亮的秘书杨X任电白县人民检察院副检察长兼反贪局长，他会查陈建明吗？为什么陈建明未进监狱一步、逍遥法外8年之久呢？事实：陈建明根本就没有进行拘留、关押过，判刑只不过是走一下法律形式而已；茂名市人民医院的医疗诊断证明实为伪造，电白人民法院没有调查取证就采纳了；后来，电白县人民检察院多次提出陈建明不符合保外就医、监外执行，也只不过迫于舆论，以这样的形式忽悠应付一下而已。建议调查现任茂名市人民检察院副检察长杨X的违法犯罪行为，陈建明逍遥法外8年就会真相大白。还有买官的有：叶X斌、梁X江、李X、陈X攀等人。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 五、行贿。杨光亮通过茂名市政法委原书记陈若培（已去世，陈绍基第一马子）认识陈绍基，向陈绍基行贿5000万元，向王华元行贿1000万元。这些行贿数字是杨光亮平时吹出来的，是否真实？请调查。如果没有陈绍基、王华元的关系，他们能帮杨光亮摆平违法的事情吗？<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 六、洗钱。杨光亮通过香港集美集团老总林X乐（又名林X，电白人，现移民英国）的博彩业洗钱，其受贿的赃款都在国外。林X乐专门为茂名地区贪官洗钱，特别是为杨光亮洗钱。林X乐是茂名市委市政府的老大，黑社会老大。为什么茂名电白的治安如此乱，其中原因是黑社会老大林X乐操纵着政府。茂名官场有这么一句话：“茂名谁说了算？林X乐」。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 七、诈骗。杨光亮幕后主持他人在电白建立农村合作基金会，以“高于银行几倍的利息」为诱饵，诈骗了电白很多人的钱。事发后，政府替农村合作基金会偿还了部分“存款”，还有很多人至今还没有拿到属于自己的一分钱，手中的“存折”成了一本废纸。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 八、杨光亮的“头号马子”，首席代言人――杨X源。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 杨X源，男，汉族，广东省电白人，祖籍电白县树仔镇文峰村委会下山锦村人，现任茂名市XX区国有资产管理公司经理，以“赌、色”闻名。杨志源经常赌博，娶了2个老婆，包养多个情妇，在深圳购买500多万元的房产，电白水东多处房产，其中一处由其侄子杨X到一家居住，一处由其兄杨X民一家居住。2009年初，杨X源挪用公司1000多万元作为赌资，并输光。杨光亮很多违法犯罪的事情都是杨X源经手的。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　九、杨光亮的能耐。杨光亮的能耐太大了。中纪委某室的副主任都是他的人，地方有他的黑社会势力。杨光亮是茂名地区黑社会保护伞。<br />
&nbsp; &nbsp; <br />
<br />
刑警支队队长程彬疑涉黑<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 另据证实，前不久被省纪委部门请走调查的还有茂名市公安局刑警支队队长程彬，以及茂港区委常委、政法委书记、公安分局局长杨强。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 现年40多岁的程彬，亦是广东电白人，是一个多月前被纪委部门带走的。原因疑是涉黑，与“李×刚案”有牵连。此前，程彬曾先后在局里任过科长、刑警支队副支队长、支队长等职务。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　　由于杨光亮等多名高官纷纷被纪委查处，且之前的2009年8月，茂名监狱原监狱长成加增、政委康烈天、副监狱长李忠淦、纪委书记李土新等原班子的多名高层，因涉嫌纵容犯人吸毒等违纪行为而落马或被依法逮捕。<br />
&nbsp; &nbsp; <br />
<br />
茂名检察院检察长跳楼身亡<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 另外，广东省茂名市检察院检察长刘先进2010年2月5日早上5时在家中跳楼身亡。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 刘先进是在位于湛江市赤坎区中山一路某大院的八楼家中跳楼身亡的。此前刘先进已经将所有后事向家人做了嘱托，并在儿子完婚次日跳楼身亡。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 2009年下半年，茂名市茂港区公安分局局长杨强、茂名市常务副市长杨光亮接连涉嫌因职务犯罪落马，使得外界纷纷猜测刘先进跳楼事件与上述案件相关。但《湛江晚报》引述湛江市公安局发布的消息称，刘先进近期以来身体一直不好，严重失眠，疑因健康原因引发精神焦虑而自杀。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 刘先进跳楼前一个月，其被调任湛江市政协某职位，虽然其在茂名市检察院检察长的职务还未被免除，但他已经不再主持检察院的工作，改由去年12月刚调任至该检察院的副检察长何平主持。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 　　刘先进出生于1951年10月，广东廉江市人，军旅出身。其于1969年2月开始任广西靖西龙州部队教导队战士、班长，后从1977年2月至1982年12月任昆明军区保卫部副营职干事，1982年12月转业到广西公安厅警卫处任科员，其后历任广西公安厅警卫处副科长、广西公安厅警卫处科长、广西公安厅警卫处副处长、湛江市公安局副局长，并于2005年2月任茂名市人民检察院代理检察长，随后开始短暂的检察长生涯。(《外参》)<br />
