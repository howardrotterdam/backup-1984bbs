													<h2>武大全国海选尖子生 或产生大师诺奖奇才</h2>
						
						
																											<div id="postmessage_851270" class="t_msgfont">荆楚网讯 昨日，备受各界关注的武大“弘毅学堂”在校内学生中进行首次人才选拔。据了解，武大开设“弘毅学堂”是为了配合教育部“珠峰计划”，由国家拨款，意在培养我国的未来“科学大师”，每年仅从全校数千名新生中择优选取50―80名学生由名师小班教学，是名符其实的武大“尖子班”。<br />
<br />
昨日最先开考的是弘毅物理班，共有48名学生从百名报名者中脱颖而出，获得了参试资格。值得一提的是，此次选拔时间从上午8时一直持续到下午6时，分为笔试与面试两个部分，虽是物理学科考试，笔试试卷却是全英文卷，考试也为开卷模式，考生不仅可自带英语字典、电子辞典、计算器、圆规等进考场，甚至可以带上物理专业书籍，翻书作答。考场内还为考生们准备了面包、火腿肠、矿泉水，供学生中午进食。据了解，此次考试主要考查方向，看考生对相关物理问题的认知，“主要考查思维模式”，因此并没有采取传统的闭卷模式。<br />
<br />
而在下午的面试中，“诺贝尔物理奖”“房价”“能源”“我国教育现状”等热点问题均入题，意在考查学生对物理学发展的眼界，对社会的认知等人文素养。<br />
<br />
据介绍，2009年，为了培养研究型杰出人才，教育部推出了“基础学科拔尖学生培养试验计划”（又称“珠峰计划”），这也是教育部为回应“钱学森之问”而出台的一项国家未来拔尖人才培养计划，由中央专项拨款支持，在北大、清华、南开大学、南京大学、上海交大、武大等16所全国著名学府进行首批“试验”，武大“弘毅学堂”便是其中之一。“弘毅学堂”共开设数学班、物理班、化学班、生物班、计算机班等5个学科班，每班仅招收学生20名。<br />
<br />
背景资料：<br />
<br />
珠峰计划是教育部主办的“基础学科拔尖学生培养试验计划”，这是一项试图破解中国人“大师”、“诺贝尔奖”心结的人才培养计划。这项由中央专项拨款提供资金支持的人才计划旨在培养创新型的领军人物。“珠峰计划”即“基础学科拔尖学生培养试验计划”，是教育部为回应“钱学森之问”而出台的一项人才培养计划。<br />
<br />
所谓“钱学森之问”，就是“为什么我们的学校总是培养不出杰出人才？”这是一道关于中国教育事业发展的一道艰深命题。2005年，温家宝总理在看望著名物理学家钱学森时，钱老曾发出这样的感慨：回过头来看，这么多年培养的学生，还没有哪一个的学术成就，能跟民国时期培养的大师相比！钱学森认为：“现在中国没有完全发展起来，一个重要原因是没有一所大学能够按照培养科学技术发明创造人才的模式去办学，没有自己独特的创新的东西，老是‘冒’不出杰出人才。”<br />
<br />
2009年10月31日，钱学森在北京逝世，享年98岁。2009年11月11日，安徽高校的11位教授联合《新安晚报》给新任教育部部长袁贵仁及全国教育界发出一封公开信：让我们直面“钱学森之问”！<br />
<br />
入选高校包括国内11所名校：北京大学、清华大学、南开大学、复旦大学、中国科技大学、南京大学、上海交通大学、浙江大学、西安交通大学、吉林大学、四川大学。该项计划首先从数学、物理、化学等基础学科开始试验。在高等教育界，“基础学科拔尖学生培养试验计划”以“珠峰计划”的代号流传，但并未大张旗鼓地宣扬。一位大学校长告诉记者，“珠峰计划”的名字比较高调，而教育部希望各校不宣传，不张扬，埋头苦干。<br />
