鄙人的感受是，“观察员”还是有的，但是他们并不是网上传说的那种签协议，领工资似的“小小特工”。基本上每个学校，各个学院，下属各种专业系别都有不同程度热爱组织并不断向组织核心靠拢的学生，他们会习惯性的将周围同学的表现，爱好，性格等等跟“辅导员”“书记”之类的老师反映，注意：这种行为是习惯性的，并且这样做的人在大学里虽然在舆论中为人不齿，但是这种行为本身并不被唾弃，相反被视为“有能力”、“能说上话”。现在的大学很像梁文道所说的，人们已经习惯了内心和外在行为施行两套标准，并且很多学生理所应当地将其割裂开来以示其“成人”、“成熟”。<br />
<br />
现在信息的流畅传播其实使得人与人之间交流的成本降低，但是沟通和争论的成本升高。那种杂揉了犬儒主义、红色思想、社会达尔文主义、当代成功学、古典厚黑的脑袋到处都是，注意是杂糅：不同比例的排列组合得出不同的个体，通常面对这样的个体很难用一个句号来结束。不求甚解，用警句来代替逻辑，用现实来代替思考可能是个永恒的病症吧。<br />
<br />
今天的很多词汇被滥用，如：“偏激”“愤青”“成熟”“老实”“能力“等等，这些人其中不乏高校教师。面对自己的学生，他们能够用一种表面貌似和蔼却极为功利的语言来阻止学生的思考，而不是引导。</div>

							
							
							
							
