													<h2>江苏近日连出“怪兆”引恐慌    专家称与地震无关</h2>
						
						
																											<div id="postmessage_455395" class="t_msgfont">江苏近日连出“怪兆” 专家称不是地震前兆<br />
<br />
2010年01月28日09:14<br />
<br />
<img src="http://img.hexun.com/2010-01-28/122517691.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
南京上空飘来“怪云”<br />
<br />
　　连续几天有市民发现，江苏、安徽、浙江上空出现“怪云”，马鞍山出现成群赤练蛇，六合古井漫水……这些“怪兆”引起了网民们的关注，一位网民还将拍摄到的“地震云” 照片传到了网络上，引起了网民们的恐慌。一位网友在龙虎社区上发帖说：“苍天，江苏不会真要大地震了吧……”这些“怪象”有科学依据吗？<br />
<br />
　　网友称，拍到了苏州上空的“地震云”<br />
<br />
　　马鞍山出现成群赤练蛇<br />
<br />
　　网友报料：1月25日11:30左右，马鞍山出现了成群赤练蛇。网友说，他发现佳山东南侧山坡上有不少蛇在爬行。有市民抓了11条赤练蛇放在一个竹篓里，被捉的蛇大的有一米多长，小的有一尺半。蛇冬季应该在冬眠，为何爬出来？许多市民认为这很不正常，是不是会有地震发生？<br />
<br />
　　【马鞍山市地震局解释】噪音影响了蛇冬眠<br />
<br />
　　昨天，马鞍山市地震局相关负责人表示，他们派出了专家第一时间赶到现场， 经调查分析，可能由于地下防空洞噪音、振动，参与早锻炼的人骚扰了蛇穴，或有其他原因，才引发了这一现象。<br />
　　江苏地震专家江铁鹰告诉记者，如果真的有大地震要来，会出现一系列怪现象。比如：鸡飞狗跳，猪跳圈，蚂蚁搬家，老鼠大搬迁等等，所有动物都会有所反应，而不仅仅只有蛇不冬眠。<br />
<br />
　　南京上空飘来“怪云”<br />
<br />
　　论坛里，“南京老许”传了一张自己拍的“地震云”照片，他说，前天下午5:45，他在家里看到天空中铺满“怪云”。他观察了半小时，还是这个样子，根本没有散开的意思。“疑似地震云。”除了南京，在一家网站上，网友说，“江苏、安徽、浙江都出现了百分之百的地震云。”“地震云”在坊间被认为是地震要来的前兆，市民们担心，是不是要地震了？<br />
<br />
　　【南京市地震局解释】属于常见的气象现象<br />
<br />
　　昨天下午，南京市地震局副局长徐徐说，到现在为止，南京没有掌握到跟地震有关的异常现象。徐徐说，现在“地震云”还没有被地震专家作为地震预报的依据，现代气象科技认为，每一种云的形成、变化都有符合科学的合理解释，它们和某种天气的演变大多有稳定的对应关系。目前，在气象观测业务规范中，还没有关于“地震云”的云状分类。之前，他们曾接到多起有关“地震云”的报告，都属于正常的气象现象，与地震没有任何关系。从本次网友的“地震云”照片看，应属于较为常见的气象现象。<br />
<br />
　　六合百年古井漫水<br />
<br />
　　六合区新篁镇营村口，百年古井突然井水满溢。以往古井的水位一直在离井口3米以下，偶尔碰到暴雨天气，老古井的水位才会上升一米左右，从未到两米以上。但最近，他们发现井水漫过井口，不停往外漫，吓了一跳。<br />
<br />
　　【南京市地震局解释】古井漫溢与地壳活动无关<br />
<br />
　　徐徐说，检测结果排除了古井漫溢现象是受地下深部因素影响造成的，与地壳活动没有关系。“古井漫溢是受到浅部影响，而不是深部的。”（快报1月 20日、21日曾作报道）<br />
<br />
　　徐徐说，不久前，他们还接到溧水一家寺庙的井水突然变浑浊的报告，赶到现场后发现，其实是不远处施工，让井水变浑浊了，也是虚惊一场。<br />
<br />
　　“网友们对地震关心是好事，我们会认真了解情况，并尽可能给大家合理解释。”徐徐说，但千万不要以讹传讹，影响自己和别人的生活。南京市地震局震情值班电话：025-86633573。<br />
<br />
　　专家观点<br />
<br />
　　江苏近期不会有破坏性地震<br />
<br />
　　专家说，江苏处在长江中下游中等地震活动带，自古以来地震活动水平就不高，历史上有记载的6.0级以上地震只有两次:1624年扬州6.0级地震和1979年溧阳6.0级地震。根据地震前兆观测，江苏省及周边没有异常反应，近期不会发生破坏性地震。<br />
<br />
　　江苏省地震局建立了测震台网与前兆台网，24小时跟踪观测。其中测震台网主要监测地震发生的震中位置、震级大小以及发生时间，一般在震后5分钟以内就能准确测定；前兆台网主要根据地壳形变、地球磁场、地球电场、流体变化等观测地震前兆，以及时作出判断。<br />
<br />
　　江苏省已建成20多个数字化监测台和60多个地震前兆监测站，各监测站点均配备了先进的地震监测设备；在南京东郊还建有国家基准地震台，与世界各国地震基准台联网，共同监测全球地震情况。<br />
<br />
　　“江南没有大地震，这是地震界的共识，也是定论。因为地质构造的缘故，江苏一般不会发生灾害性大地震。”江苏地震专家江铁鹰说。<br />
<br />
（来源：现代快报）<br />
