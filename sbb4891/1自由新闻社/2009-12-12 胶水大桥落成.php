																											<div id="postmessage_369455" class="t_msgfont">南京新建大桥开裂 施工单位用胶水糊上<br />
2009-12-12 09:00:00　来源: 南方日报(广州)　<br />
<br />
核心提示：近日，南京耗资5000万新建一年的汉中门大桥，被市民发现有三十多根栏杆裂开了口子。而在接到投诉后，施工单位竟连夜用胶水将裂口糊上了，欲盖弥彰的“伤疤”，让人们惊叹“豆腐渣”工程登峰造极。<br />
<br />
 <br />
裂缝能塞下一只手<br />
<br />
<br />
 <br />
桥栏杆用胶水“糊”上了<br />
<br />
<br />
南方日报12月12日报道&nbsp;&nbsp;见过“糊弄”人的,没见过这么“糊弄”桥的!继重庆出现“楼裂裂”后,近日,南京耗资5000万新建一年的汉中门大桥,被市民发现有三十多根栏杆裂开了口子。而在接到投诉后,施工单位竟连夜用胶水将裂口糊上了,欲盖弥彰的“伤疤”,让人们惊叹“豆腐渣”工程登峰造极。<br />
<br />
花费5000万改造闹市区便民桥 <br />
<br />
据当地媒体报道,这个月,有市民发现,横跨秦淮河的汉中门大桥“有点不对劲”,南侧的几十根栏杆都出现裂纹,并且裂得很规则,他们担心是桥梁受力的结构出现问题,于是向媒体报料,希望相关部门立即调查。<br />
<br />
据了解,这项工程属于改造项目。由于施工的难度较大,工程从2007年开始,一直到2008年6月才竣工。据工程负责人称,整个项目总投资大约5000万元。去年《南京日报》在报道汉中门大桥通车新闻中称,改造后的大桥“桥面将出现12股机动车道,将明显增强桥面车流的通行率”。<br />
<br />
一年后30多根栏杆根部出现大裂口 <br />
<br />
但是今年12月,陆续有附近市民发现,桥上很多栏杆出现裂痕,而且裂缝的走向有规则,与拱桥桥面的“八”字形相符合。有退休工程师看了之后担心:“这不是普通的热胀冷缩,而是结构上出现问题。桥面受力出现问题,那后果可能非常严重啊!”<br />
<br />
8日,南京《现代快报》记者来到现场查看发现,大桥南侧人行道的花岗岩栏杆都出现裂纹,裂纹在栏杆的根部,约有30多根栏杆都出现了这样的裂纹。有的裂口缝隙宽到足以塞进成年男子的手掌。<br />
<br />
当地记者联系了南京市市政设施养护管理处,工作人员表示会与桥梁维护单位联系,并称,“在没调查清楚原因前,也不敢武断下结论就是质量出问题,导致结构损坏,也有可能是热胀冷缩等原因造成”。<br />
<br />
<br />
南京建委道歉网友斥责“雷人” <br />
<br />
据当地媒体报道,南京市建委目前已紧急出台汉中门大桥整改意见,并对施工单位擅自采用胶结材料封闭裂缝,在社会造成不良影响表示道歉。南京市建委表示将追究相关人员责任,绝不护短。<br />
<br />
由于市民及时发现以及媒体的报道,南京汉中门大桥没有造成不良后果。但是不少网民还是斥责这种豆腐渣工程太“雷人”。有网友在新华网上发表评论,“周岁桥梁多处开裂竟用胶水糊,试问良心裂缝该用啥粘呢”?<br />
