南京再现胶水糊桥 市政称安全没问题(组图)<br />
2009-12-17 04:34:47　来源: 新华报业网-扬子晚报(南京)　跟贴 458 条 手机看新闻 <br />
核心提示：南京汉中门大桥改造竣工一年之后，出现30处断裂，施工方竟然胶水糊桥。此事余波未平，昨天，有市民报料称，红旗桥也出现了多处裂缝，最大裂缝能塞进手掌，有的缝隙已经用水泥糊住，至少有五处裂缝用胶水糊起来了。<br />
<br />
<br />
用水泥粘。<br />
<br />
<br />
<br />
栏杆的裂缝能伸进一根手指。<br />
<br />
<br />
<br />
用胶水糊。<br />
<br />
扬子晚报12月17日报道 南京汉中门大桥改造竣工一年之后，出现30处断裂，施工方竟然胶水糊桥。此事余波未平，昨天，又有市民向记者报料称，从南京城东进入市中心的主干道长乐路上的红旗桥也出现了多处裂缝，裂缝和汉中门大桥类似，有的缝隙可以塞进去手指，有的缝隙已经用水泥糊住，有的还仍然裂着。<br />
<br />
红旗桥栏早就开裂了<br />
<br />
昨天，记者接到市民的举报电话称，和前两天媒体上报道的鼓楼区汉中门大桥一样，位于秦淮区的红旗桥也出现了多处裂缝，有的地方能放进去一根手指，有的地方栏杆和立柱之间已经完全分离，没有任何附着，而几乎每一个栏杆和立柱之间都有水泥的痕迹，明显是修补过的。<br />
<br />
接到市民的反映之后，记者来到了位于武定门外的红旗桥。市民陶先生告诉记者，红旗桥历史悠久，应该是“文革”期间修建的，他印象中最近的一次修缮应该是和“武定门”城墙一起修的，大概也就是几年前。至于桥栏杆裂缝的问题，陶先生表示，他每天都走过整座桥再坐公交车，裂缝不是最近才出现的，应该是早就有的了。陶先生表示，出现裂缝时他也曾向有关部门反映过，后来也来人检查过，都是栏杆上的裂缝。<br />
<br />
最大裂缝能塞进手掌<br />
<br />
随后，记者对整座红旗桥进行了统计，发现几乎每一座桥栏杆和立柱的接缝处，都有被水泥涂抹过的痕迹，应该是在安装桥栏杆的时候，施工方用来加固的。再仔细观察栏杆的表面，也有不少地方都出现了不同程度的裂纹。出现裂缝最严重的地方，在桥南北两边各有一处，都是从栏杆的立柱开始开裂，立柱的根部已经出现断裂，一直延伸到地面。其中最大的裂缝有两处，都在桥尾，开裂的宽度足可以让一根手指完全塞进去，最宽的地方一只手掌都可以塞进去。<br />
<br />
也用胶水“糊弄”<br />
<br />
自从媒体报道了汉中门大桥出现裂缝之后有关单位用胶水糊桥的事情之后，市民对于眼前这座红旗桥的开裂也十分重视，纷纷质疑桥梁的施工方，这座桥是否也同样存在着质量问题？而和汉中门大桥存在同样问题的桥栏杆开裂是否也会影响到桥身的主体结构？而记者在现场还发现，对于一些裂缝，有关单位曾经用水泥进行过填补，但同时，记者还发现用胶水粘桥的始作俑者并非是汉中门大桥，在红旗桥上，至少有五处裂缝用胶水糊起来了。在一处栏杆下方花岗岩装饰图案上，图案的一角似乎已经脱落，不过上面有一道长长的黄色水渍，看起来似乎是用胶水重新粘连起来的。“这一块摸上去很光滑，就是胶，应该是拿胶水补上的。”在现场一位市民自称自己曾经安装过一些桥栏杆，他表示粘裂缝的胶水不是一般的胶水，这么做肯定是应急之用，但最好的方案还是将出现裂缝的栏杆整体换掉。<br />
<br />
市政：桥梁安全没问题<br />
<br />
记者随后来到了南京市建设委员会，将桥体开裂的事情进行了反映。建委一位工作人员表示，会立即安排有关部门进行调查。昨天下午，市政公用局就此事召开了媒体通气会，记者了解到，红旗桥的主桥于1969年5月竣工，1998年进行了拓宽改造，安装了现在的这种花岗岩栏杆，建设单位为南京风光建设综合开发公司，监理单位为江苏城建市政监理公司南京一监部，施工单位为南京水利建筑总公司。<br />
<br />
在接到记者的反映之后，市政公用局就组织技术人员到现场进行调查，从调查情况来看，红旗桥在栏杆建设方面确实存在问题，而且这种问题应该是在2005年以后出现的。“在2005年全市桥梁普查中，红旗桥评定结果为B级（良好），桥梁栏杆未发现问题。”南京市市政设施综合养护管理处的张副处长告诉记者，根据初步调查来看，该桥栏杆存在的问题并不影响桥身主体结构，桥梁安全是没有问题的。<br />
<br />
建栏杆的可能不是建桥的<br />
<br />
张副处长告诉记者，2009年11月，红旗桥有些栏杆开裂约1厘米，栏杆立柱有三根出现裂缝。随后，市政公用管理局就指派技术人员到现场察看，初步分析出来的原因是桥梁改建使用已经10年，局部可能出现疲劳损坏，另一个原因就是桥梁混凝土的伸缩系数和花岗岩栏杆的伸缩系数不一致，容易造成变形位移量不一致。桥梁横向栏杆和立柱之间出现裂缝，属于桥梁热胀冷缩的正常现象，但是由于当初施工不当，使得横向栏杆和立柱之间完全连在一起，热胀冷缩产生的拉力很容易就会将花岗岩立柱拉断。此外，将栏杆断裂部位用胶水粘起来，也是当初选材的时候遗留下的问题。因此，可以说在当初改建加装花岗岩栏杆的时候，对桥梁这一块考虑比较周全，但是对栏杆，考虑不周全。换句话说，建桥梁的可能是一个施工单位，但是做栏杆的又是另外一个单位。而做花岗岩的这支施工队不懂桥梁建筑，加之其他原因才可能出现现在的问题。<br />
<br />
将排查全市桥梁隐患<br />
<br />
<br />
市政公用局表示，经过现场的察看和分析研究，现在的红旗桥在使用上不存在安全隐患，能保证正常通行。同时，桥梁安全也一直是市政部门密切关注的问题及相关工作重点，近期市政部门已经安排行业各单位对所管辖桥梁进行一次隐患排查，待专项排查结果形成后，将有计划地进行针对性维修。<br />
<br />
新闻回顾： 南京新建大桥开裂 施工单位用胶水糊上(图) <br />
<br />
近日，南京耗资5000万新建一年的汉中门大桥，被市民发现有三十多根栏杆裂开了口子。而在接到投诉后，施工单位竟连夜用胶水将裂口糊上了，欲盖弥彰的“伤疤”，让人们惊叹“豆腐渣”工程登峰造极。<br />
