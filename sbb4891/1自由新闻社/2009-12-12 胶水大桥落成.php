报道说，这项工程属于改造项目。由于施工的难度较大，工程从2007年开始，一直到2008年6月才竣工。据工程负责人称，整个项目总投资大约5000万元。但是今年12月，陆续有附近市民发现，桥上很多栏杆出现裂痕，而且裂缝的走向有规则，与拱桥桥面的“八”字形相符合。有退休工程师看了之后担心:“这不是普通的热胀冷缩，而是结构上出现问题。桥面受力出现问题，那后果可能非常严重啊!”<br />
<br />
南京《现代快报》记者在现场查看发现，大桥南侧人行道的花岗岩栏杆都出现裂纹，裂纹在栏杆的根部，约有30多根栏杆都出现了这样的裂纹。有的裂口缝隙宽到足以塞进成年男子的手掌。对此，南京市政设施养护管理处工作人员表示，会与桥梁维护单位联系，并称：“在没调查清楚原因前，不敢武断下结论就是质量问题，导致结构损坏，也有可能是热胀冷缩等原因造成。”<br />
<br />
据报道，南京市建委目前已紧急出台汉中门大桥整改意见，并对施工单位擅自采用胶结材料封闭裂缝，在社会造成不良影响表示道歉。南京市建委表示将追究相关人员责任，绝不护短。但不少网民还是斥责这种豆腐渣工程太“雷人”。<br />
<br />
