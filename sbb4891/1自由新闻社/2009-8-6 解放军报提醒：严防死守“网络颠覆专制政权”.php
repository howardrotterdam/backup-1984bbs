													<h2>解放军报：Twitter的网络颠覆力不容小觑</h2>
						
						
																											<div id="postmessage_193826" class="t_msgfont">将近午饭时间，在中新网看到脑残文一篇，饱了。先把TWITTER墙掉，再说它不好，死无对证，无耻之极。<br />
<br />
2009年08月06日 11:02　来源：解放军报<br />
&nbsp; &nbsp;&nbsp; &nbsp; 自从互联网进入大众化、全球化时代，网络“无疆界”、“零距离”、“即时性”的特性便被有意识地用于进行国家间的政治文化渗透。与以往常用的电台、电视、报纸等工具相比，互联网不仅传播速度快、范围广、信息量大，而且可以匿名、匿源传播，成本低廉，隐蔽性好，灵活性强，不仅降低了“舆论攻击”和渗透的门槛，也有利于一些政府和团体掩饰自己的介入。由于西方发达国家在相当程度上掌握着网络的技术制高点，控制着网络的连接权、话语权，因此在网络渗透和颠覆方面具有先天优势。近年来，不少发生政治动荡的国家不约而同地指责来自“外部敌对势力”的网络威胁，认为这是造成动荡局势的重要根源之一，而事实也正是如此。<br />
<br />
网络颠覆手段浮出水面<br />
<br />
　　今年4月7日，摩尔多瓦大选后发生的那场未获成功的“颜色革命”又被称为“Twitter革命”。因为Twitter――美国一个微型博客社交网站为此次“革命”提供了一个“小而强大”的平台。摩尔多瓦的青年组织“HydePark”和“ThinkMoldova”策划了此次“Twitter革命”，其网站得到了美国国务院文化和教育局的资金支持。“ThinkMoldova”领导人之一纳塔利娅・莫拉里在自己的博客中这样描述：“6个人，只用了10分钟的快速思考便做出决定，然后用数小时通过网络、‘脸谱’、博客、短信和电子邮件将消息传播出去……结果1.5万名年轻人走上街头。”哈伊・莫斯科维奇事发当天一直在通过Twitter网站对示威活动进行图文报道。第二天，他和同伙们通过“脸谱”和Twitter网站向外传播消息时，还特地为消息制作“＃pman”的标签，这几个符号正是摩首都基希讷乌市中心广场的罗马尼亚语缩写。当网络被切断后，莫斯科维奇就用群发短信的方式散布信息。据称，摩尔多瓦“Twitter革命”的幕后推手是金融大鳄乔治・索罗斯，他经常利用Twitter等网站，在美英看不顺眼的国家制造动乱。索罗斯开放社会研究所还安排了专门的技术人员，负责向“相关人员”传授如何利用互联网在“封闭社会”推动“民主运动”，以推翻“专制政权”。而且，他们的目标不只是摩尔多瓦，还有塔吉克斯坦、叙利亚和泰国。尽管摩尔多瓦“颜色革命”最终没有成功，但充分显示了社交网站在造谣惑众、制造混乱方面的“别样作用”。<br />
<br />
　　Twitter等社交网站再次显示出巨大的“煽动力”，是在今年6月份的伊朗大选，它成为策划、煽动伊朗社会动荡的重要推手。美英和以色列情报部门通过Twitter、“脸谱”、Youtube等网站，散布耸人听闻的消息让伊朗民众“中毒”，并引发强烈不满情绪。首先，在选举当晚散布消息称穆萨维获胜，于是当几小时后内贾德宣布胜选时，看上去就像一个大骗局；随后，一些社交网站和微型博客的用户开始接收到一些关于政治危机和街头抗议行动的似真似假的匿名消息，主要是枪击和大量人员死亡等“骇人听闻”的消息，而这些消息直到现在都未得到证实；与此同时，中情局还指使美英等国的反伊朗政府者继续煽动混乱局面。处于混乱中的普通民众，无法分辨Twitter上信息的真实性，没人清楚信息发布者到底是德黑兰抗议活动的目击者还是中情局特工。其实，这正是美国中情局想要的效果，其目的就是制造更大的混乱，让伊朗人内讧。<br />
<br />
　　“箭袋中的一支新箭”<br />
<br />
　　对于Twitter、“脸谱”等社交网站在摩尔多瓦及伊朗动荡中所起到的“关键作用”，西方表现得颇为兴奋，因为他们发现，在互联网上，“一种新的、强有力的力量正在生成。”伊朗危机证明了Twitter已成为一种强有力的政治工具，这种既可以影响伊朗，又不使自己深陷其中的方式正是一些西方国家所需要的。<br />
<br />
　　美国许多政客就非常注重推行“网络外交”，重视利用社交网站“脸谱”、视频共享网站Youtube、图片共享网站Flicdr和Twitter这些平台传递外交政策信息，声称要以网络软管理来应付那些打压国内媒体的国家。美国防部长罗伯特・盖茨就坦言，Twitter等社交媒体网络是美国“极为重要的战略资产”，奥巴马政府已经把社交网站视为“箭袋中的一支新箭”。<br />
<br />
　　2009年6月23日，美国防部长盖茨正式下令成立网络战司令部，新建成的司令部隶属于美国十大联合司令部之一的战略司令部，主要职责是“保护美国军用网络系统不受外国攻击，并对美国攻击别国网络系统的能力加以整合”。美国政府的专门研究报告《关于美国获得和使用网络攻击能力的技术、政策、法律与道德问题》称，网络攻击的范围很广，既包括小规模冲突，也可能是全面战争。在战场上，网络攻击可以用来抑制敌人的防空能力，破坏指挥、控制和通信系统，瓦解智能武器。报告指出，网络攻击还包括一些较为隐蔽的行动，可以用于旨在影响政府、事件、组织或个人的秘密行动。<br />
<br />
　　网络攻防对美军而言并非新概念，各军种内部早已建立了从事网络攻防的专门单位。不仅直属国防部的美国国家安全局、国防信息系统局有自己的网络战部队，美战略司令部也在2005年成立了由顶级电脑专家和黑客组成的“网络战联合功能司令部”，空军2008年成立了专门负责网络攻防的第24航空队。陆军也早已建立计算机应急反应分队，重点是对付战术层次的电脑威胁，必要时发起网络攻击，侵入别国军事网络。海军则在“舰队信息战中心”成立了“海军计算机应急反应分队”。目前，美军共有3000～5000名信息战专家，50000～70000名士兵涉足网络战。如果加上原有的电子战人员，美军的网络战部队已达到9万人之巨。<br />
<br />
　　未雨绸缪提早防范<br />
<br />
　　互联网信息发布、传播的自由性，决定了其信息必然是鱼龙混杂，很容易被别有用心者利用，以诱导和放大社会矛盾，甚至引发动荡和骚乱。7月5日，新疆乌鲁木齐发生严重暴力犯罪事件。事前，以民族分裂分子热比娅为首的“世维会”就在网络上歪曲事实，竭力污蔑中国民族宗教政策，煽动一小撮“疆独”分子上街游行、示威，鼓动他们“要勇敢一点”、“要出点大事”。这一事件再次表明，加强网络监管，发展网络安全力量已成为维护社会稳定和国家安全的迫切需要，必须采取有力措施，有效抵御网络攻击特别是各种颠覆性犯罪活动，避免互联网成为敌对势力的一个新的“毒箭”。<br />
<br />
　　由于西方特别是美国拥有对互联网域名根服务器的监控权，加上在计算机硬件和软件上的极端垄断地位，西方国家已牢牢掌握了国际互联网的控制权。5月30日，微软宣布不再为古巴、朝鲜、叙利亚、苏丹和伊朗5国用户提供MSN接入服务，理由是这5国被美国政府列入了禁止提供授权软件服务的被制裁国家名单。微软的“小试牛刀”为世界敲响了警钟：在制海权、制空权、制天权之后，制网权将成为左右未来国家安全的另一战略制高点。这并非耸人听闻，虚拟空间的“多国演义”正在上演。目前，除美国外，俄罗斯、日本、印度、韩国等国都在积极准备应对所谓“第六代战争”，把“网络防御―攻击”视为保卫国家利益的重大问题。西方一些国家故意虚构、炒作“中国网络间谍”，并渲染中国黑客的“官方背景”，则表明他们已将中国视为其网络战的重要目标。<br />
<br />
　　网上的攻防没有硝烟，但却可能造成实实在在的巨大损失，必须切实做好互联网的防御，尤其要确保金融、电力、国防等核心专网的安全。欲达此目的，就要尽快摆脱美国在互联网领域的软件和硬件控制，加快网络核心技术的自主化进程，下决心集中力量攻关，扶持国内网络核心技术及产品的推广，争取国内终端早日用上性能可靠的“中国芯”和自主产权的操作系统。要加强网络防护技术研究，提高隔绝、屏蔽、锁定、反击网上恶意攻击的能力。只有这样，才能真正形成互联网防御的“铜墙铁壁”。<br />
<br />
