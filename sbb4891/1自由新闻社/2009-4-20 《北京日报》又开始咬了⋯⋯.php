<br />
中国经济的确创造过增长奇迹，国防、人民生活也取得了不少进步，但要论民主体制变革的速度、规模、幅度与深刻程度，台湾的政治社会变化更是举世公认的奇迹。邓小平的确在大陆发展过程中扮演了重要角色，但要论个人对整个国家、民族的历史贡献，蒋经国更是扭转乾坤的伟人：一位引领人民把独裁专制封建的旧台湾，改造成独立自主、积极自信的新台湾；又带领台湾人民从危难局面突围，走上民主开放之路。这些发展奇迹，这些历史伟人，怎么没见北关先生去歌颂、去铭记，却要独独“记住毛泽东”呢？其用意何在呢？<br />
<br />
细看该文，作者先是要人们记住不可逆转的世道人心，接着要人们记住毛泽东对两岸对立与大陆闭关自力更生的功绩，最后话锋更进一步，““自由”、“民主”，确实都是好字面，但在现实中，是不是真的“好东西”，还要看条件、看效果”。这就一下子点明了，中国特色，恐怕才是北关先生念兹在兹的重点。熟悉这位精英的人都知道，北关先生喜欢拿他的中国特色来衡量中国的一切。戴着这样的有色眼镜，一方面，他对自由民主的批评指责十分严苛，什么事都要以“中国特色”为标准来衡量和解剖一把；另一方面，对那些扯着改革开放发展旗号批评甚至歪曲、诋毁普世价值的言论，他却显得十分宽容。我们不能不说，北关先生，您只怕是陷入了一种以你们党价值为依归的意识形态偏执。<br />
<br />
