为什么不能记住蒋经国？　2009-04-20 18:07<br />
分类：默认分类 字号： 大大&nbsp;&nbsp;中中&nbsp;&nbsp;小小 按：去年是《北京晚报》，今年是《北京日报》，站在所谓政治正确的高度试图用文革以及扣帽子的手法抹黑长平以及他所属的南方报业集团。报业同行基本是有共识的，在具体事实上尽可以商榷，但一般避免直接攻击对方的理念。所以，这两次北京的报业集团出手打击南方报业集团，是件很不可思议的事。<br />
应该说这两次南方报业都算是吃了个哑巴亏，也可能是不屑于对这种诛心加扣帽子的文章做出回应。但笔者还真是有点看不惯，正好与南方报业算是颇有渊源，算是强出头一次。而且这次我想证明的是，即使在当下的话语体系中，北京方面的这种言论也是有很大的可商榷处。所以，我这次用当下流行的话语与逻辑来回应，文字固然显得迂腐，但更多的是证明这些人是一种观念的僵尸，甚至连现阶段政府的执政理念之高度都远远没有达到。这叫做以子之矛攻子之盾。<br />
<br />
<br />
《北京日报》发表了一篇署名北关的奇文，题目叫做《长平为何要记住蒋经国》。先解释一下，长平是一个人的笔名，他写了一篇文章，叫做《记住蒋经国》，回顾了蒋经国先生的一生。北关先生不但从字里行间看出了长平先生“通篇怀想赞美之辞，树碑立传的意味尽显无遗”，而且隔海相望、高瞻远瞩的指出：“台湾民众对蒋的怀念并没有作者所说的那么高调和隆重”，并且从政治正确的高度两面下手，说“长平先生这样一位生活在大陆、尽享新中国阳光雨露和改革开放发展成果的所谓“媒体精英”，竟然要从海峡对岸一个独裁专制者身上寻找中国社会发展的想象空间，简直是咄咄怪事！”<br />
<br />
应该说最后这招最狠。如果你认可前面的阳光雨露与成果，那你在蒋经国先生身上寻找中国社会发展的想象空间，自然就是罪大恶极的事。而如果你不认可阳光雨露的话，这是往我们执政党身上抹黑，是阶级异己分子。当然，所谓阳光雨露这句话虽然颇有文革时期的风范，实际上回顾我们这些年的历史来说，倒不如说是“阳光雨露、莫非君恩”。这话一般是被贬职的官员在叩谢皇帝圣旨时候说的。<br />
<br />
其实这倒还不是重点，重点的是这个逻辑上看上去给人设了一个圈套，实际上在追求小范围政治正确的同时，把自己放在了更大的政治不正确的范畴里。<br />
<br />
我们知道，同为炎黄子孙，渴求统一的盼望都是很殷切的。如果说国民党当年与共产党是老对手的话，现在在寻求两岸共识的基础上，对双方领导人的评价都已经出现了颇大转变。就拿抗日战争来说，大陆已经非常爽快的承认了国民党正面抗战的牺牲与功绩，而对于国民党的现任领导人，也表示了更大的好感与敬意。在学术界当中，蒋经国先生在最后取消了“动员堪乱”的戒严体制，重新回到了正常的社会建设上来，也得到了相当的肯定。<br />
<br />
如果想用政治正确来压人一头的话，这是不是更大的政治正确？“历尽劫波兄弟在，相逢一笑泯恩仇”的大环境之下，“独裁专制者”出于这么一份北京市官方党报之手，于我们的统一大业是否能够有帮助？<br />
<br />
在此奇文中，北关先生于偷换概念的运用，大致到了一个炉火纯青的程度上。他在列举长平先生文中“历史将会无可争议地记载，蒋经国一生最大的功绩，是他超越个人、家族和党派的私利，解除了戒严令，开放了结社自由和言论自由，带领台湾结束了专制统治，推开了自由民主的大门”后，直接就说到这是长平先生念兹在兹的重点是“西式的政治制度”。<br />
<br />
如果我们再次从政治正确的角度来看的话，自由与民主也是我们的政府一直在强调的重点，怎么一提到这两个词，就能够让北关先生直觉的反应到西式的政治制度上去呢？难道我们中国人民就没有享受民主与自由的权利么？这不但与当前的政治话语向违背，并且也否认了我们《宪法》当中的表述。这是最大的政治不正确。<br />
<br />
要是我们纵观该报的整个版面，头条评论的标题《制度的优劣要由实践来检验》，而北关先生的文中强调：““自由”、“民主”，确实都是好字面，但在现实中，是不是真的“好东西”，还要看条件、看效果。”从表面上看，可谓是呼应紧密。头条评论的标题与内容大致说的是泰国最近发生的事情，以此来证明民主与自由在某些时候并不一定很美妙。但是，这个实践是在泰国，并非发生在我国。我国从来都强调有自己特殊的国情，与其他国家并不完全相同。如果我们承认这个前提的话，我们也要进行自己的实验来检验才是。而北关先生的话语中，已经否定了现实中民主自由会有好的效果。在政治不正确的状态下，一个版面的文章都在互相打嘴巴不止，不知道是编辑的能力问题，还是其政治素养不过关了。<br />
<br />
