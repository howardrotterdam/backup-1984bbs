　　<br />
　　记者问赵是否后悔将此事在微博上曝光，赵没有回答，只说不会再把事情的后续发在微博上了。因为“这是我的家事”。<br />
　　<br />
　　此后，潇湘晨报滚动新闻记者又拨通了赵庭景美好友易天的电话。易天是赵庭景美第一时间想起要求助的对象――在微博上。<br />
　　<br />
