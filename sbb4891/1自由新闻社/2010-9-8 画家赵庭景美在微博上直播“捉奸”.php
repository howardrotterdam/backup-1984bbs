红网长沙9月8日讯（潇湘晨报滚动新闻记者 曾鸣）在这个喧哗的时代，要制造骚动并不容易。最终，赵庭景美在面对一地内衣和捉奸现场时，选择了微博。<br />
　　<br />
　　捉奸在床 直播开始<br />
　　<br />
　　9月8日早上，女画家赵庭景美推开家门，看到散落一地的男女内衣，还有“裸在床上”的老公与另一名女子。此情此景令她崩溃，她随后的反应是――争吵、打架、发微博。<br />
　　<br />
　　赵庭景美是一名微博使用者。在过去七个多月里，她关注了647个人，有4181个粉丝。在已发的3002条微博中，主题包括美食、感情、艺术展、山西黑煤窑。最近的更新里，她奉上了将自己男人捉奸在床的直播。<br />
　　<br />
　　用赵庭景美的话来说，事情的缘起是这样的：<br />
　　<br />
　　“早上我回家，看到床上@北京陈青蓝和@爱伺机摸人裸在我的床上。”<br />
　　<br />
　　这是典型的微博体。其中“@北京陈青蓝”和“@爱伺机摸人”皆为微博用户名，分别是她的丈夫陈青蓝和第三者许宇萱。<br />
　　<br />
　　赵庭景美的捉奸微博一共九条，这是第一条，时间是9月8日早上7点5分。<br />
　　<br />
　　在接下来的3个小时，赵庭景美和丈夫与小三厮打，在派出所做记录。如果时光倒流两年，这些事情都是私密的家庭纠纷，不会有第四个人知道。<br />
　　<br />
　　但接下来赵庭景美开始发泄情绪――“极度崩溃”，并向微博好友求助，还奉劝“爱伺机摸人”：“一定要找个可靠的男人，很显然，陈青蓝不是。他向你承诺的物质条件，必须要由我来替他实现，你觉得这可能吗？”<br />
　　<br />
　　这一切都以微博的形式记录下来。截止到8日下午7时，这九条微博得到了4930次转发，还有2582条评论。<br />
　　<br />
　　网友关注 人肉搜索<br />
　　<br />
　　事件在微博上不胫而走。微博的特征是，任何一个用户说了任何一句话，都会立即显示在微博的首页。理论上，在微博的世界里，个人可以向全世界喊话。<br />
　　<br />
　　众多微博用户开始关注此事，人肉搜索随机启动。微博网友发现，陈青蓝除了北京某报社记者身份外，还是“铅笔经济研究社”理事；而“爱伺机摸人”原名“许宇萱”，亦是铅笔经济研究社的一员。<br />
　　<br />
　　稍后，“爱伺机摸人”在豆瓣上的征婚启事被微博用户人肉出来。在这份发表于2009年10月的启事中。爱伺机摸人对对象列举了16点要求，第一条：“年纪大点没关系，70后最好，但是要单身。年纪小点也没关系，90后也成，但是要有钱。”<br />
　　<br />
　　“爱伺机摸人”在被人肉出后，并未紧张，有微博用户告知其“红了”，“爱伺机摸人”回复”谢谢”，并辅以亲吻表情。<br />
　　<br />
　　更多澄清 更多关注<br />
　　<br />
　　9月8日12点47分，陈青蓝通过微博作出回应：”谢谢各位围观群众的支持！没什么好看的。请各位朋友不要耽误自己工作。”<br />
　　<br />
　　不期该回应引起更大关注，在两小时内，该条微博被转发294次，得到评论520条。更多的微博用户参与围观。“妖中之妖”称，”一点都不耽误！我每天除了八卦就是等下班了！今天真有收获！<br />
　　<br />
　　当日，15点27分，陈青蓝通过微博再次回应：<br />
　　<br />
　　“关于绯闻事件，声明几点：1、@爱伺机摸人是神赐给我的礼物，是我用半生时间寻找到的最爱，如果她愿意给我机会，我会给她最完整的幸福。2、@爱伺机摸人昨晚在我家住，但我们没有发生性关系。3、我不是吃软饭的。相反，这个家的开支都是我在承担，房子是用我的钱买的。4、我和@赵庭景美已闹离婚一年。”<br />
