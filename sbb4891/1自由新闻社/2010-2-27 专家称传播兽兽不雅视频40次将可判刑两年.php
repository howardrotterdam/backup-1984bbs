													<h2>专家称传播兽兽不雅视频40次将可判刑两年</h2>
						
						
																											<div id="postmessage_495275" class="t_msgfont">据东莞日报报道从2月下旬开始，车模翟凌(艺名兽兽)的不雅视频开始在网上疯传，并被称为兽兽门事件。专家表示，根据本月刚出台的“两高”司法解释，不雅视频传播40次以上，就算违法，最高可获刑两年。<br />
<br />
QQ传递成主要获取渠道<br />
<br />
“你看了兽兽门没有？”在南城一公司工作的“飞翔的翅膀”(网名)昨天一到单位，就开始有同事和网友跟他谈起兽兽门视频，而整整一天，跟他聊起相同话题的同事不下10人。<br />
<br />
从前日开始，兽兽门视频开始在网上疯传，而记者也在谷歌热榜看到，2月24日和2月25日，8个最热门关键搜索词，有6个是同兽兽门有关。<br />
<br />
由于兽兽门视频涉嫌不雅，不少网站已经对其相关链接进行屏蔽，网友们从何渠道获取？记者随机调查发现，除了少数网友是在24日之前从天涯等网站下载外，超8成网友是通过QQ等即时聊天工具，从朋友或者网友处获取。<br />
<br />
非牟利目的也触犯法律<br />
<br />
不少市民更关心的是，传播兽兽门视频是否涉嫌违法或者犯罪？<br />
<br />
东莞网络文化协会秘书长林环告诉记者，2月3日，新华社播发了《最高人民法院最高人民检察院关于办理利用互联网、移动通讯终端、声讯台制作、复制、出版、贩卖、传播淫秽电子信息刑事案件具体应用法律若干问题的解释(二)》，并于当日开始施行。<br />
<br />
根据新规定，以牟利为目的，利用互联网、移动通讯终端制作、复制、出版、贩卖、传播淫秽电影、表演、动画等视频文件20个以上的，可按照我国刑法第363条第1款规定，处3年以下有期徒刑、拘役或者管制，并处罚金。<br />
<br />
但不是以牟利为目的的网友也不要存在侥幸心理，新的司法解释规定，不以牟利为目的，利用互联网或者移动通讯终端传播淫秽电子信息，如果数量达到规定标准的两倍以上的，也就是发送40个视频以上，并造成严重后果的，以传播淫秽物品罪定罪处罚，情节严重的，处两年以下有期徒刑、拘役或者管制。<br />
<br />
新司法解释还强调，利用聊天室、论坛、即时通信软件、电子邮件等方式，实施上述行为的，亦以传播淫秽物品罪定罪处罚。<br />
<br />
在30人以上QQ群传播违法<br />
<br />
记者调查发现，不少市民和网友获取视频，是通过QQ的群邮件功能或者MSN的聊天组，但专家表示，这样做也有法律风险。<br />
<br />
根据新的司法解释，利用互联网建立主要用于传播淫秽电子信息的群组，成员达30人以上或者造成严重后果的，对建立者、管理者和主要传播者，依照刑法第364条第1款的规定，以传播淫秽物品罪定罪处罚。<br />
