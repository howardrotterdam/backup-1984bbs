21日，温家宝来到深圳博物馆参观《改革开放总设计师邓小平》大型展览，向邓小平铜像献花。 新华社记者 刘卫兵 摄<br />
<br />
<br />
<br />
新京报8月22日报道&nbsp; &nbsp;今年是深圳经济特区建立30周年。8月20日至21日，温家宝在中共中央政治局委员、广东省委书记汪洋陪同下到深圳调研，走访了互联网、生物、软件等行业的多家企业和社区、口岸。<br />
<br />
<br />
<br />
“要使人们生活有安全感”<br />
<br />
温家宝20日晚召开会议，听取广东省和深圳市工作汇报。温家宝强调，过去30多年中国的发展变化，靠的是改革开放，未来要实现中华民族的伟大复兴，仍然要靠改革开放。<br />
<br />
他强调，只有不断解放思想，与时俱进，全面而持久地推进改革，才能建成完善、成熟、有中国特色的社会主义。不仅要推进经济体制改革，还要推进政治体制改革。没有政治体制改革的保障，经济体制改革的成果就会得而复失，现代化建设的目标就不可能实现。要保障人民的民主权利和合法权益；要最广泛地动员和组织人民依法管理国家事务和经济、社会、文化事务；要从制度上解决权力过分集中又得不到制约的问题，创造条件让人民批评和监督政府，坚决惩治贪污腐败；要建设一个公平正义的社会，特别是要保障司法公正，重视保护和帮助弱势群体，使人们在生活中有安全感，对国家的发展有信心。<br />
<br />
<br />
他最后指出，必须永远牢记，只有坚持推进改革开放，国家才有光明前途。我们要头脑清醒、明辨是非，坚定信念、增强信心。要继续解放思想，大胆探索，不能停滞，更不能倒退。停滞和倒退不仅会葬送30多年改革开放的成果和宝贵的发展机遇，窒息中国特色社会主义事业的勃勃生机，而且违背人民的意志，最终只会是死路一条。<br />
<br />
