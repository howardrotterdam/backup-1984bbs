													<h2>温家宝：只有坚持改革开放 国家才有光明前途</h2>
						
						
																											<div id="postmessage_791696" class="t_msgfont">2010年08月21日14:49&nbsp;&nbsp;来源：中国政府网<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 今年是深圳特区建立30周年。中共中央政治局常委、国务院总理温家宝近日在深圳考察工作并听取广东省和深圳市工作汇报。温家宝强调，经济特区30年来的实践充分表明，改革开放是决定当代中国命运的关键抉择，是实现国家强盛、人民幸福的必由之路。过去30多年中国的发展变化，靠的是改革开放，未来要实现中华民族的伟大复兴，仍然要靠改革开放。<br />
<br />
　　20日至21日，温家宝在中共中央政治局委员、广东省委书记汪洋陪同下，深入企业、科研院所、口岸和社区，就加快转变经济发展方式、发展战略性新兴产业进行调研。每到一处，温家宝都和广大干部群众亲切交谈，共商深圳改革发展大计，展望经济特区美好前景。20日晚，温家宝主持召开会议，听取广东省和深圳市工作汇报并作重要讲话。<br />
<br />
　　温家宝说，深圳经济特区成立30年来，各项事业都取得巨大成就，社会面貌发生了翻天覆地的变化，创造了世界工业化、城市化发展史上的奇迹。深圳不仅迅速改变了自身的面貌，而且对全国的改革和发展做出了重要贡献。深圳经济特区的发展变化，是改革开放以来中国社会沧桑巨变的缩影，是中国特色社会主义强大生命力的生动体现。<br />
<br />
&nbsp; &nbsp; 他强调，经济特区不仅要继续办下去，而且要办得更好。经济特区必须立足新起点，适应新形势，瞄准新目标，增创新优势，实现新跨越。一要坚持“特”。要结合自身条件和特点，创造性地开展工作，在体制改革、发展方式转变、和谐社会建设等方面创造新的特色。二要追求“好”。就是在确立和实现未来发展目标、任务时，强调“好字当头”、“好字优先”、“好中求快”。三要突出“新”。要坚持把创新当做特区的灵魂和生命，向创新要发展动力，向创新要发展优势，向创新要发展资源，向创新要发展空间。四要敢争“先”。要发扬“敢为天下先”的精神，抢抓机遇，争先进位，争当推动科学发展的示范区、构建和谐社会的首善区、改革开放的先行区。五要立足“干”。要发扬“不等、不靠、不空谈、不争论、不折腾”的优良传统，勇于实践，敢于开拓，用干事创业的实绩，把特区的旗帜举得更高。<br />
<br />
　　温家宝指出，社会主义初级阶段的长期性，要求我们坚定不移地推进改革开放。我国现代化建设取得了举世瞩目的成就，但我国还处于并将长期处于社会主义初级阶段，实现现代化的宏伟目标需要坚持不懈的奋斗。必须通过深化改革开放，清除影响经济社会发展和人的全面发展的各种体制障碍，最大限度解放和发展生产力，为国家现代化建设提供不竭的力量源泉。<br />
&nbsp; &nbsp; 他强调，完善社会主义制度的艰巨性，要求我们坚定不移地推进改革开放。我国社会主义基本制度具有巨大的优越性，但是各方面体制机制还不够完善。只有不断解放思想，与时俱进，全面而持久地推进改革，才能建成完善、成熟、有中国特色的社会主义。不仅要推进经济体制改革，还要推进政治体制改革。没有政治体制改革的保障，经济体制改革的成果就会得而复失，现代化建设的目标就不可能实现。要保障人民的民主权利和合法权益；要最广泛地动员和组织人民依法管理国家事务和经济、社会、文化事务；要从制度上解决权力过分集中又得不到制约的问题，创造条件让人民批评和监督政府，坚决惩治贪污腐败；要建设一个公平正义的社会，特别是要保障司法公正，重视保护和帮助弱势群体，使人们在生活中有安全感，对国家的发展有信心。<br />
<br />
　　温家宝指出，破解发展中的各种难题，要求我们坚定不移地推进改革开放。我国在短短几十年内走过了一些资本主义国家两三百年才走完的历史进程，发达国家在工业化、城市化、市场化、国际化进程中遇到的矛盾和问题我们都会遇到，而且会更加集中、更加尖锐。在发展中遇到的矛盾和问题，必须用改革的办法、发展的办法加以解决。<br />
&nbsp; &nbsp; 他强调，顺应经济全球化趋势、更好地统筹国内国际两个大局，要求我们坚定不移地推进改革开放。中国几千年的发展历史告诉我们：只有开放兼容，国家才能富强，闭关锁国必然落后和挨打。必须坚定不移地实行对外开放的基本国策，更好地利用国内国外两个市场、两种资源，大胆学习借鉴人类社会一切文明成果，在扩大开放中推动我国经济社会改革和发展。<br />
<br />
　　温家宝最后指出，我们站在一个新的伟大的历史起点上，肩负着更加光荣而神圣的任务。必须永远牢记，只有坚持推进改革开放，国家才有光明前途。我们要头脑清醒、明辩是非，坚定信念、增强信心。要继续解放思想，大胆探索，不能停滞，更不能倒退。停滞和倒退不仅会葬送30多年改革开放的成果和宝贵的发展机遇，窒息中国特色社会主义事业的勃勃生机，而且违背人民的意志，最终只会是死路一条。在这个关乎国家前途和命运的大事上，我们不能有丝毫的动摇。<br />
