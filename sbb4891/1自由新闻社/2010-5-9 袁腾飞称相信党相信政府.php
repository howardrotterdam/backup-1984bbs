																											<div id="postmessage_607442" class="t_msgfont">因钱翻脸对簿公堂 沈浩波与袁腾飞谁更&quot;无耻&quot;？<br />
2010年05月10日 07:54:51 　来源：北京晨报<br />
<br />
<br />
&nbsp; &nbsp; 《百家讲坛》主讲人袁腾飞和其著作《历史是个什么玩意儿》的出版商北京磨铁图书有限公司因版税问题发生纠纷，以至于对簿公堂，一时间闹得沸沸扬扬。<br />
<br />
&nbsp; &nbsp; 据称，袁腾飞因不满出版商迟迟不付版税并且隐瞒印数，又在未授权之下出版新书，遂将其告上法庭。而出版商方面则以书店尚未汇款为由，表示无法结算。<br />
<br />
&nbsp; &nbsp; 作者与出版商的关系历来爱恨交织，据了解，鲁迅先生及冰心先生都曾遭遇过此类纠纷。在当今，由于出版业的逐渐开放，大量民营出版商进入出版业，致使出版业鱼龙混杂，各种纠纷更是层出不穷。<br />
<br />
&nbsp; &nbsp; 记者采访了资深业内人士――作家出版社主任编辑王宝生。王宝生表示，现在的出版业纠纷，很多时候并不是那么简单，作者、出版商应各打五十大板。<br />
<br />
&nbsp; &nbsp; 作者一定清白吗？<br />
<br />
&nbsp; &nbsp; 对于这一事件，王宝生表示具体的情况并不了解，所以很难就事件本身下结论。不过通过王宝生记者了解到当前出版业中的种种现象和问题。<br />
<br />
&nbsp; &nbsp; 王宝生说：“在出版社工作，和专家学者，包括百家讲坛的主讲人在内的很多人接触过。并不是所有的作者都是善良的。当今时代是一个商业时代，在商品大潮之下，有些作者自己会把作品当作一个商品，一个赢利的目的和手段。特别是现在的社会环境，它在有意无意地推动或者挑动作者的欲望，挑动知识分子对金钱的欲望，把他们变成了逐利的商人，包括媒体，喜欢报道那些版税很高的事件。在这样的环境之下，一部分作家自然受到影响，极个别的可能还会变本加厉，好像出版社就必须遂他所愿。实际上这是一种非理性的自我膨胀，在错误的认识之下产生的不平衡心态，对于文化本身来说，并非好事，甚至是一种悲哀。”<br />
<br />
&nbsp; &nbsp; 当然，王宝生所说，只是少数，王宝生表示：“其实大部分作家还是好的，特别是一些大作家，越有名气的，在这一方面就越豁达，比如高满堂、余秋雨、海岩等，他们给出版社的合约只有一个自己的签名，把版税、印数、甚至装帧等这些东西全部交给出版社做。作家这么做，反而使得出版社不能轻忽，要做得更好。”<br />
<br />
&nbsp; &nbsp; 出版商为什么毁约？<br />
<br />
&nbsp; &nbsp; 既然各打五十大板，出版商自然也非无可指摘。王宝生表示，可能大的出版社在执行合约的方面会比较好，因为这涉及声誉等很多问题。但一个小的出版商可能就没太多的制约，出现种种问题在所难免。<br />
<br />
&nbsp; &nbsp; 王宝生说：“现在的图书市场，是一个无序竞争的市场。大量的民营出版商进入出版业，对于出版业来说，根本上需要一种正确引导。但不可否认的是，个别出版商常常以赢利为单一目的，违反商业规则、商业道德等事件也不鲜见。”<br />
<br />
&nbsp; &nbsp; 现在的图书市场并不景气，王宝生说：“盗版、无序竞争等问题都是出版业不可忽视的，能够赚钱的书并不多，大部分的书并不赚钱，有些还会亏损，遇到亏损怎么办？正规的出版社可能会仍旧执行协议，因为出版社是文化企业，赢利固然要追求，但同时还需保证出版社本身的公信力、影响力。但是一些出版商特别是小的出版商，他只是逐利的，不管其他，一本书赚钱了，他可能会按照合约付给作者报酬，但要是亏钱了呢？这就要考验出版商的诚信了，是不是会执行合约，很难说。此外，因为逐利，个别出版商还会因作者缺乏相关的知识而利用合约漏洞欺骗作者，瞒报印数等。”<br />
<br />
&nbsp; &nbsp; 王宝生表示：“作者和出版社是一个合作关系，有合作就必然有纠纷，我想不管是作者还是出版社，最重要的应该是诚信，大家做到双赢多好，问题是现在经常出现的是单赢，这就是纠纷多的原因。”<br />
<br />
&nbsp; &nbsp; 图书行业缺乏诚信<br />
<br />
&nbsp; &nbsp; 在袁沈纠纷中，沈浩波以书店未回款为由延迟版税交付。对此，王宝生认为是可能的，他说，对于出版社和出版商来说，和书店之间的关系确实很复杂。<br />
<br />
&nbsp; &nbsp; 王宝生说：“如果说要规范出版市场，其实最应该规范的是书店，特别是民营书店。出版业现在是买方市场，很多时候出版社都不得不看书店的脸色行事，比如说结账时间，一般是6到8个月甚至1年，书店卖了书才给钱，有些甚至卖了书都不给钱。即便如此，在供货时，供货商还要提供诸多的折扣、促销等价格上的让步。”<br />
<br />
&nbsp; &nbsp; 这种情况在民营书店更加常见，王宝生说：“国营书店好一些，总会付钱，但是民营书店不一样，它赚到钱就是个人的。但现在民营书店占据半壁江山，供货商即便是做了很多让步仍旧没有办法。出版社还好一点，出版商要货款就更难了。我想，出版是讲文化、讲底蕴、讲时间积累的，不管是作者还是出版社，多少年的努力才能做出好东西，因此，诚信的合作对于文化事业来说尤为重要。” （周怀宗）<br />
<br />
&nbsp; &nbsp; ■新闻链接<br />
<br />
　　因钱翻脸对簿公堂<br />
<br />
&nbsp; &nbsp; 事件的起因是袁腾飞和出版商磨铁图书公司之间的利益纠葛，袁腾飞诉称，2009年 5月11日，他和北京磨铁图书有限公司签订了为期1年的合同，规定了版税的标准，以及重印、再版是按实际销售额计算。合同签订后，磨铁图书公司给了袁腾飞 100万元的预付款。去年8月和12月，《历史是个什么玩意儿》的第一册和第二册相继出版。今年2月，袁腾飞要求磨铁图书公司提供这两册图书的印数证明并支付版税时，对方没有回应。袁腾飞的代理律师说：“《历史是个什么玩意儿》印了多少、卖了多少、版税多少我们一点也不知道。”磨铁图书公司对作者没有诚意，所以袁想解约。<br />
<br />
&nbsp; &nbsp; 磨铁图书公司则认为，“截至5月6日接到法院传票前，我们已经预付给袁腾飞200 万元。所以对于他的起诉理由和诉求都不认可。”袁腾飞的代理律师表示：“袁确实又收到100万元。如果他们认为是预付款，我们不予接受；如果是后来支付的版税，我们会在起诉中予以扣除。”磨铁图书公司老总沈浩波在接受采访时则表示，书店根本没有这么快给他回款，在回款未到之前，他只知道书发出去多少，而不知道卖了多少，所以根本无法与袁腾飞结算。袁腾飞是过于心急了。</div>

							
							
							
							
