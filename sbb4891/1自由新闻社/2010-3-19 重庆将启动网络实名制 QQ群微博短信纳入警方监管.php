<br />
中国媒体周四（3月18日）引述重庆市政法委书记刘光磊说，警方将构筑信息网络防控网，以虚实结合的方式加强对网络的控制和引导。<br />
<br />
据刘光磊介绍，重庆市共有600万网民，平均每两人拥有一部手机。他说，面对如此庞大的群体，必须加强管理与控制。<br />
<br />
刘光磊还说，除了实行网络防控网外，市民的QQ群、微博客和手机短信等，也将被纳入警方监控范围。<br />
<br />
此外，重庆市还决定，全市在2012年底安装电子摄像头50万个，将警方所说的治安复杂行业与重点部位均纳入视频监视范围。<br />
<br />
目前正面临打黑除恶艰巨任务的重庆市开始大抓对网络的控制引起有关人士的关注。<br />
<br />
评论人士认为，这表明，在共产党眼中，日益增长的网络影响和黑社会性质的犯罪团伙具有同样的威胁，共产党要在重庆试行一种监控言论自由的新方法。（bbc）</div>

							
							
							
							
