													<h2>重庆将启动网络实名制 QQ群微博短信纳入警方监管</h2>
						
						
																											<div id="postmessage_522375" class="t_msgfont">2010年03月19日 03:05 重庆晚报<br />
<br />
本报讯 昨日，重庆市政法委召开学习贯彻中央政法委书记周永康在全国人大会议上的讲话精神动员大会。相关负责人透露，为构筑网络防控网络，警方将启动网络实名制，QQ群也将被纳入监管范围。<br />
<br />
昨天，市政法委书记刘光磊介绍，中央政法委书记周永康在参加十一届全国人大三次会议重庆代表团审议时，肯定了“打黑除恶”专项斗争，提高了执法司法公信力。刘光磊说，为贯彻落实这一精神，我市“打黑除恶”斗争将长期进行下去，坚持做到露头就打，坚决铲除黑恶势力滋生土壤。随后，警方将在全市范围开展百日攻坚战，呈高压态势打击“两抢（抢劫、抢夺）一盗”多发性侵财犯罪。<br />
<br />
“警方将构筑信息网络防控网。”刘光磊称，目前，我市有600万网民，每2人就有一部手机，面对如此庞大的社会群体，必须采取虚实结合的方式加强管理和引导。按照年初部署，我市警方将出台网络管理相关法规，明确网络运营企业和网民的责任，逐步启动网络实名制，构筑一张网络防控网，市民QQ群、微博客、手机短信等，都将被纳入警方监管范围。所谓网络实名制，是指将上网者的身份和其真实姓名、身份证号等相对应联系及统一的制度。<br />
<br />
除此之外，还在全市范围建成1000个平安建设基层示范点。2012年底，全市安装电子摄像头50万个，治安复杂行业、重点部位都将被纳入视频监控范围。<br />
<br />
记者 徐其勇</div>

							
							
							
							
