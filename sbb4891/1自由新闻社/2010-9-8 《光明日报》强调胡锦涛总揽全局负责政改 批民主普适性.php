													<h2>《光明日报》强调胡锦涛总揽全局负责政改 批民主普适性</h2>
						
						
																											<div id="postmessage_818966" class="t_msgfont">中科院政治学所所长：要扬弃西方民主制度 划清两种民主界限是民主政治建设的前提<br />
2010年09月08日&nbsp;&nbsp;光明日报<br />
<br />
如何正确认识民主，是关系社会主义现代化建设和民族复兴的大事情。新中国成立60多年来，特别是改革开放30多年来，中国取得了历史性的伟大进步。现在世界上越来越多的人为中国经济的飞速发展发出由衷的赞叹，也有一些人开始关注、探寻“中国奇迹”背后政治制度的原因。中国政治建设的核心是中国特色社会主义民主政治建设。中国特色社会主义民主政治是在当代中国建设和发展的新型民主政治。这种民主是社会主义的，是符合中国国情、符合中国优秀文化传统、符合中国社会发展进步要求的政治制度。同时，这种民主也是对人类有益政治文明成果的借鉴和发展。从这个意义上讲，中国特色社会主义民主政治是一种伟大的制度创新。制度创新，既要善于借鉴又要勇于发展。借鉴不是盲目的照抄照搬，而是对可资借鉴的制度与理念的理性扬弃，去粗取精、去伪存真，取其精华、去其糟粕。中国的民主政治建设就是要进行这样的扬弃，尤其是对西方资本主义民主进行扬弃。透彻认识是理性扬弃的关键，因此，推进中国民主政治建设的前提就是要分清中国特色社会主义民主同西方资本主义民主之间的界限。<br />
<br />
中宣部理论局编写的《划清“四个重大界限”学习读本》，对于中国特色社会主义民主同西方资本主义民主的界限作出深刻而准确的阐述，抓住了两种民主最根本的区别，观点鲜明、分析深入、说理透彻，是目前关于这一问题最具理论深度的权威论述。《读本》仅仅用了不足万言的篇幅，便对这一重大理论问题作出了清晰的梳理，简约而不失深刻，通俗而富有启发。<br />
<br />
<strong>《读本》针对一些人宣扬西方资产阶级民主具有普适性和永恒性的错误看法</strong>，从西方民主自身发展的历程，说明无论是在内容上还是形式上，民主都是具体的、历史的、相对的，是性质不同的政治制度。世界上从来没有放之四海而皆准的民主制度，也没有一成不变的民主发展道路和民主模式。<br />
<br />
《读本》系统总结了中国特色社会主义民主的鲜明特征和独特优势。新中国成立以来，特别是改革开放以来，中国取得的一系列辉煌成就，从根本上证明了中国特色社会主义民主政治的独特优势。中国特色社会主义民主有利于发挥人民群众建设和管理的积极性、主动性和创造性；有利于国家政权机构协调高效运转，形成 “集中力量办大事”的制度优势；有利于保障国家的统一稳定，促进社会的团结和谐。<br />
<br />
《读本》明确指出了中国未来政治体制改革和民主政治建设的方向。中国特色社会主义民主政治建设历来与政治体制改革紧密相连。<strong>《读本》引用胡锦涛总书记的论述：“核心的问题不是政治体制要不要改、而是朝着什么方向改”，进一步明确指出，中国政治体制改革要坚持发挥党总揽全局、协调各方的领导核心作用</strong>；要切实保证国家的一切权力属于人民，扩大人民群众享有的各项民主权利，健全民主制度，丰富民主形式，拓宽民主渠道；全面落实依法治国基本方略，不断完善法律体系，推进社会主义法治国家建设。《读本》强调，要通过方向正确的政治体制改革，不断推进社会主义民主政治制度化、规范化、程序化，使中国特色社会主义民主政治的优越性得到进一步发挥。<br />
<br />
《读本》不仅正面阐述了中国特色社会主义民主政治的特色与优势，讲清了坚持走中国特色社会主义政治发展道路的必要性。同时也旗帜鲜明地指出，中国绝不搞西方“三权分立”的政治体制、多党竞争轮流执政的政党制度。这样就进一步明确了中国特色社会主义民主与西方资本主义民主的界限，有利于读者从正反两方面加深对这种界限的认识和理解。<br />
<br />
正确认识和划清中国特色社会主义民主与西方资本主义民主的界限是十分重要的。马克思曾经说过：“全部问题在于确定民主的真正意义。如果这一点我们做到了，我们就能对付民主，否则我们就会倒霉。”我们要坚定不移地走中国特色社会主义民主政治道路，这是一条光明的道路，是一条能为中国人民带来权利、尊严和幸福的道路，也是一条需要不断探索的道路。沿着这条道路不断开拓前行，必须澄清模糊认识，排除错误干扰，自觉划清中国特色社会主义民主同西方资本主义民主的界限。而《划清“四个重大界限”学习读本》，可以在这方面发挥很好的作用。<br />
<br />
(作者系中国社会科学院政治学所所长方宁)</div>

							
							
							
							
