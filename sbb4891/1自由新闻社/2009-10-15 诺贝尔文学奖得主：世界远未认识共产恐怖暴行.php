&nbsp; &nbsp; 附注： <br />
<br />
&nbsp; &nbsp; 2009年诺贝尔文学奖颁给了罗马尼亚裔德国诗人、小说家和散文家赫塔・米勒（Herta Muller，56岁）。赫塔-缪勒1953年出生在罗马尼亚一个讲德语的少数民族家庭，1987年她与丈夫迁居德国。1982年，穆勒发表了其“处女作”――一本名为《低地》的短篇小说集。 <br />
<br />
&nbsp; &nbsp; 罗马尼亚曾是前苏联共产主义政权的东欧卫星国之一，在苏联解体之后，这些卫星国家纷纷独立。 <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;<strong> 活着就是胜利 </strong><br />
<br />
&nbsp; &nbsp; 在铁腕独裁的齐奥塞斯库（Ceausescu）统治下，人民莫不生活在水深火热之中。一直到1989年东欧发生剧变，才结束了此一血腥、高压的统治。罗马尼亚人在九死一生中想出一句悲伤的感言：“活着就是胜利。” <br />
<br />
&nbsp; &nbsp; 据后来者忆述当时的惨况，“商店全是空架子，食品极为匮乏、难得，粮食的供应要靠排长龙获得。要得到日常所需，必须通过个人关系或行贿。此外，全国到处有秘密警察。” <br />
<br />
&nbsp; &nbsp; 秘密警察是独裁专制政权的共生物，据描写，“到处都有这类人存在，甚至连老师、同学、邻居，也有替政府当鹰犬。所以人人自危，说话要格外小心。” <br />
<br />
&nbsp; &nbsp; 米勒在她的一篇文章中写道：“在独裁政权时期，每天都有人越过边境逃出去，很多人去了西方。也有更多人下落不明，也许是死于乱枪扫射之下，因此，边境成了死亡区，也是乱葬岗。” <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; <strong>因拒绝合作而失业 </strong><br />
<br />
&nbsp; &nbsp; 1973年到1976年，缪勒在罗马尼亚蒂米什瓦拉一所大学学习罗马尼亚和德国文学。在大学学习期间，她加入了巴纳特行动小组。巴纳特行动小组是一个讲德语青年作家组织，反对当时罗马尼亚领导人奇奥塞斯库的统治，寻求言论自由。 <br />
<br />
&nbsp; &nbsp; 1976年，完成大学学业后，缪勒在一家机器工厂当翻译。由于不愿充当秘密警察的线人，拒绝合作，缪勒被工厂解雇。被解雇之后，她受到了秘密部门的骚扰。此后，缪勒一直在几家幼稚园教书和当德语家教，直到1987年离开罗马尼亚。 <br />
<br />
&nbsp; &nbsp; 1987年是米勒生命的转折点，她与小说家瓦格纳（Wagner）结婚，次年就不断接到国内外大学的邀请，到处去讲学。1995年，她开始写诗，作品得过诗歌奖和其他多项荣誉。目前她住在柏林。 <br />
<br />
&nbsp; &nbsp; 1997年，她退出地位崇高的德国笔会，她之所以退出笔会，只因不满它与前东德民主组织合并。2008年米勒写了一封公开信，给目前的罗马尼亚文化机构主席，以抗议机构提供赞助给两名奸细。 <br />
<br />
&nbsp; &nbsp; 除不停地写作、出书，她也常常发表“反罗马尼亚”的抗议文章。2007年，她写了一篇《罗马尼亚的集体健忘症》文章，向欧盟提出警告：罗马尼亚的独裁政治还没完结，在那里，“他们只是装成一切已在空气中消失，全国人民正陷入于一种健忘症。”这是在齐奥塞斯库倒台17年后还存在的事实。她在文章中也大胆揭露官商勾结、贪污枉法的行为，“每日都在发生”。 <br />
<br />
&nbsp; &nbsp; 有人在评述她的小说时说，书中的人物，尽显如何在一个不安全、不正常的时代中，想方设法去贿赂和适应，以求活下去的本能。也因为这样，正面的人物只好千方百计扭曲自己，甚至铸成自己人生的大错。所写人物多为老师、学生、农民、工程师和许许多多的平民百姓。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;<strong> 我怕故我写 </strong><br />
<br />
&nbsp; &nbsp; 获得2009年诺贝尔文学奖的罗马尼亚裔德国女作家赫塔・米勒说过：“我怕故我写”。 <br />
<br />
&nbsp; &nbsp; 1945年二战结束后，大量罗马尼亚人被流放到苏联，缪勒的妈妈就在其中，她母亲曾被流放到苏联乌克兰劳改，多年之后，缪勒在其作品《呼吸摆动》(2009年)中描写了流放到苏联的罗马尼亚人的生活。 <br />
<br />
&nbsp; &nbsp; 缪勒自己在离开公职后，家里频频被罗马尼亚的秘密警察侵入检查。她的很多男女朋友，可以在莫明其妙的情况下，一夜之间神秘失踪。她常常自问：“我为何还能活在人世？” <br />
<br />
&nbsp; &nbsp; 赫塔・缪勒，她以写作罗马尼亚裔的德国人在苏俄时代的遭遇著称，代表作品：《我所拥有的我都带着》、《光年之外》、《行走界线》与《河水奔流》。 <br />
<br />
&nbsp; &nbsp; 瑞典文学院的评审认为，米勒的作品：“以诗歌的凝练和散文的率直，描写了失业人群的生活”。她的得奖作品，真实地反映了罗马尼亚人民在独裁者齐奥塞斯库铁掌下的生活境况。 <br />
<br />
&nbsp; &nbsp; 虽然赫塔・缪勒在22年前离开暴政统治下的祖国――罗马尼亚，但至今，她还在不断揭露和关心极权统治下的国家和人民。 <br />
