无独有偶，同样是被所谓的男朋友强奸致死的连丽丽，在历经数次的法医检验中，也是始终没有“发现”和“记载”导致机械性窒息的相应体表损伤，案件的侦破最终还是取胜于案犯的口供。试想，如果没有这个口供，情况又将如何？难道面对所谓的悬案疑案，共和国的刑警们只可能依赖口供缉拿凶犯吗？<br />
