																											<div id="postmessage_181265" class="t_msgfont">王雪梅：根据黄静母亲提供的资料，就黄静案谈几点个人意见<br />
(2009-07-13 22:58:22)<br />
<a href="http://blog.sina.com.cn/s/blog_4be55c380100dzhg.html" target="_blank">http://blog.sina.com.cn/s/blog_4be55c380100dzhg.html</a><br />
<br />
一，黄静宿舍并非死亡的第一现场，而是移尸现场<br />
黄静案共启动七次法医鉴定，其中一次因心脏等器官丢失且保存的尸体已经腐败而终止鉴定合同。<br />
根据笔者审阅黄静母亲当面递交的先后六次法医鉴定文书，不难看出，尽管后三次的法医专家已经否定了前三次公安机关认定的冠心病、风心病、肺梗死的死因鉴定，但是，有一个事实却是所有参与检验并得出不同结论的法医全都不得不承认的，那就是在黄静的尸体上真正能够让我们清清楚楚看到的导致死者死亡的直接死因只有一个――急性肺水肿！<br />
只要粗通医学，就可以明白一个不需要证明的常识性问题，那就是能够让一个原本具有交换气体功能的肺脏变成一个充满液体并因为这些液体而丧失了呼吸功能最终导致死亡的人，在事件发生的整个过程中一定是要垂死挣扎奋力抗争的，这个过程即不可能像某种猝死来的那样闪电般的迅猛，也不可能无声无息，更不可能直到死亡身体还保持着仰卧位，特别是被子还整整齐齐地盖过鼻梁。没有3到5分钟的时间，无论是自身疾病还是外界暴力，都不可能让一个鲜活的生命死于突发的急性肺水肿。而一个明明白白死于急性肺水肿的人，在死亡之前那段极为痛苦的缺氧过程中，无论是出于人体的生物本能，还是出于生物的求生欲望，无论如何都不可能平静地卧在床上，更不可能把自己的口鼻都盖在被子下面静悄悄而又乖乖地等待着死亡的降临！<br />
然而，黄静母亲提供的资料，却让我看到了一个非常平静的死亡现场，死者黄静平卧在自己的床上，床上的床单很平整，一床被子整齐地盖过她的鼻梁。经过我的反复询问，黄静母亲肯定地告诉我，这个证言就是撬开黄静宿舍门锁首次走进黄静尸体现场的人最初的陈述。<br />
如果这个证据是可靠的，那么，毫无疑问，黄静的尸体在撬锁人到来之前一定曾经被一个活人精心地布置过，因为尸体上急性肺水肿这个特殊的语言告诉我们，黄静不可能在极度缺氧的状态下再把自己的口鼻用被子捂住，也不可能在呼吸极度困难的过程中，保持宁静中的仰卧位直到死亡！<br />
根据最高人民法院人民法院司法鉴定中心法医学鉴定书载明，“尸体检验所显示被鉴定人黄静双下肢N窝处较大范围皮下出血及表皮剥脱、以及右下肢膝关节内侧皮肤皮下出血属于生前损伤，符合手指直接压迫、摩擦所致损伤的特点”，结合被黄静母亲高度怀疑的对象（以下简称“被怀疑对象”）的自述，我们完全有理由认定一个事实，那就是黄静生前不仅曾经遭遇到“被怀疑对象”用手强行扳开大腿的暴力侵害，并且也曾经遭遇到“被怀疑对象”骑跨在自己胸部的暴力行径，这样的暴力行径不仅造成一个柔弱女孩双下肢多处皮下出血及表皮剥脱，而且在相当程度上导致了致死性的急性肺水肿。假如这样的暴力侵害行为真的发生在深更半夜的学校宿舍楼内，假如黄静母亲手中的确握有与黄静同住一个楼的同事提供的事发当夜只听到黄静宿舍有脚步声和门响声，却没有听到任何挣扎搏斗喊叫呼救声，那么，我们就有足够的理由怀疑黄静宿舍根本就不是什么死亡现场，而是个移尸现场。<br />
罪犯移尸的目的非常明确，那就是刻意把一个强奸过程中的暴力死亡，掩饰为一个性游戏中的意外猝死！<br />
