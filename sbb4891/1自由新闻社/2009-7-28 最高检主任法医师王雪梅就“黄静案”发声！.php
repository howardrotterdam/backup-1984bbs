																											<div id="postmessage_181262" class="t_msgfont">王雪梅：但愿如此轻率的“排除”不再发生！<br />
（2009-07-19 14:07:38）<br />
<a href="http://blog.sina.com.cn/s/blog_4be55c380100e2y1.html" target="_blank">http://blog.sina.com.cn/s/blog_4be55c380100e2y1.html</a><br />
