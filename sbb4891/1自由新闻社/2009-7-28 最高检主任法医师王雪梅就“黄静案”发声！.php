&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;在黄静案发生以及后来的多次鉴定直到审判期间，我“恰巧”被高检院党组“安排”由技术中心“借调”到侦查监督厅“待命”。借调之前，我的领导明确告诉我，组织不仅将为我争取一个侦监厅副厅长的名额，还准备将我在技术中心分管的原班人马编制进来。其实，当时的我并不糊涂，对组织安排的真实意图也非常清楚，这个意图就是不惜一切代价，让王雪梅同志听组织的话为组织服务。<br />
&nbsp; &nbsp; 其实，作为一名有着35年党龄的党员，听组织的话为组织服务理所当然，但是，在不算太长的职业生涯中，我却时常表现出不听组织话不为组织服务，而是令人匪夷所思地听死人的话为死人服务的行为。之所以在工作中出现这样怪异的现象，原因很简单，因为我是一个肩负着天职的职业法医，因为我爱这个党更爱这个国家！<br />
&nbsp; &nbsp; 实话实说，和组织抗争是一件很痛苦很劳累的事情，我这人天生怕苦怕累，实在有些厌倦和惧怕了这样的工作环境。所以，八年前，尽管明知组织真实的意图并非重用我和我所领导的那支技术队伍，我也还是特别高兴地欣然接受了组织的安排。我假装傻乎乎地在侦监厅一呆就是好几年，近乎白痴似地在机关大院白吃白喝。在侦监厅的几年里，我只办理那些领导们需要我办理的案件，而对检察机关技术部门职责范围内的案件则不闻不问，不管不顾，道理很简单，因为我的组织已经在形式上让我彻底离开了这个岗位，我在精神上心理上已经不需要对这个行业存在的任何问题承担任何责任了。<br />
&nbsp; &nbsp; 需要声明的是，我王雪梅绝非如同某些媒体和某些网民吹捧的是什么时代的英雄和楷模，更不是什么维权勇士。事实上，从小到大再到老，吃喝玩乐享受生活始终是我生命中最为重要的一个内容。如果道德上允许一个人只向社会索取而不需要向社会提供服务的话，我会用全部的时光在吃喝玩乐中傻乎乎地享受一辈子人生。说句大实话，我王雪梅既不是真傻，也不是真英雄，既然我的组织特意在给了我很高的待遇和地位之后，又给了我大把大把享受生活的时间和空间，既然组织不需要我承担本应承担的历史责任，我这个加入组织多年的人，干嘛非要成心和强大的组织作对呢？直到今天，就在此时此刻，我也还是非常怀念也非常留恋职业生涯中那段既不伤害自己又不伤害组织的美好时光。<br />
&nbsp; &nbsp; 在侦监厅，我过得很愉快也很轻松，我的工作内容不再与组织发生冲突，我不再坚持做与组织对立的事情，组织也决不会强迫我做自己认为不应该做的事情，这样的工作环境让我充分感受着生活的美好。可惜，美中有些不足的是，我依旧时常收到来自全国各地的申诉信，从这些申诉信中，我意识到中国司法鉴定体制存在的问题越来越难以掩饰，我知道我和我的同行们根本就无力阻止事态的继续恶化。有鉴于此，我不仅不急着让组织兑现当初的承诺，反到一而再再而三地强烈要求高检院党组撤销我在技术中心的行政职务。其实，这样做的目的绝不是为了成心恶心我的组织，而是因为我不想在实际权力被他人控制的情况下，却要为别人承担着历史的责任甚至罪过。<br />
&nbsp; &nbsp; 也许组织为我安排的那场好戏实在没有办法继续演下去了，也许组织终于识破了我的“阴谋鬼计”，于是，在“待命”或者说借调了近5年之后，我又再次被宣布重返技术中心。<br />
&nbsp; &nbsp; 这下子我可就有些惨了，虽说组织仍然想尽一切方法剥夺我实际工作的权利，但是，我却重新背负起承担全国检察机关刑事技术工作领导职务的名分，这个名分经过媒体过分的吹捧和宣传，让我不得不重新做起了和组织对立并且以命抗争的事情。<br />
&nbsp; &nbsp; 还需声明的是，我是个厌恶政治厌恶权利的人，我从不过问与自己职责范围无关的事情，在专业上，我只关心自己职责范围内必须关心的法医鉴定。事实上，谢佩银、代义当然还有黄静的死因鉴定，都是我这个最高人民检察院主任法医师必须应该倾注心血关注的案件。当然，一旦对这些通天大案公开发表了个人意见，我定会毫不犹豫地以生命作抵押，而绝不会只是随便说说而已！<br />
&nbsp; &nbsp; 纵观黄静案的相关资料，最终的审判的确存在着显而易见的问题，但是，在我个人看来，出现这些问题的根源不在于主审法官，而在于法医学鉴定人，尤其是参加最高人民法院组织鉴定的几位法医鉴定专家。<br />
&nbsp; &nbsp; 让我们一起看看由最高人民法院组织全国的法医专家出具的鉴定结论：“被鉴定人黄静在潜在病理改变的基础下，因姜俊武采用较特殊方式进行的性活动促发死亡。”<br />
&nbsp; &nbsp; 根据这一鉴定结论，法院判决书认定：“没有黄静的潜在病理改变原因，黄静也不会死亡。可见，被告姜俊武的行为与被害人黄静的潜在病理改变是造成死亡的共同原因。”<br />
我个人认为，法官对鉴定结论的理解不存在本质上的问题。换句话说，古今中外，任何一个法官都只能对这样的鉴定作出基本相似的推断。当然，是否构成强奸，那是案件定性问题，不是我们讨论的死亡原因问题，不在我们今天的讨论范围之内。<br />
&nbsp; &nbsp; 然而根据黄静母亲提供的资料，在最高人民法院组织的鉴定中，名列第一位签名人的国内某著名法医病理学专家，却在审判之后对自己出具的鉴定结论提出了与法官完全不同的解释。<br />
&nbsp; &nbsp; 从这位病理专家的录音谈话中可以知道，他认为导致黄静死亡的原因是胸部受压引起的机械性窒息，他明确指出潜在病理改变不是导致黄静死亡的原因。<br />
&nbsp; &nbsp; 我个人认为，这位专家对自己出具的鉴定结论进行的解释，既不符合鉴定结论的文字表述，又违背了客观事实和科学规律。<br />
&nbsp; &nbsp; 在法医的死亡档案中，胸腹部联合受压导致的机械性窒息并不罕见。但是，每一个法医都应该知道一个常识，那就是出于人类的生存本能，一个正值青春年华的女孩子，在遭遇到致死性的胸腹部外力后，绝不会活活地等着别人把自己压成致命性肺水肿，即使平日里十分柔弱的女孩，在面临生死存亡的紧要关头，也必然会拼足了全身的气力，迅速挣脱压在自己胸腹部的巨大压力，竭尽所能地摆脱死亡的威胁。现在，我们所面对的客观事实是，黄静需要奋力挣脱压在自己身上的那个巨大压力，仅仅只不过就是一个男人，而且，这个男人仅仅只是用自己身体的部分重量压在了黄静的胸腹部。假如黄静生前根本就不存在参与死亡过程的潜在疾病，假如黄静是一个基本健康的女孩子，那么，在那样一种与死亡抗争的应激状态下，挣脱仅仅压在自己胸腹部的那个男人对自己致死性的压迫，并非是件难以完成的事情。可以肯定地说，出现在黄静尸体上的致命性肺水肿，绝不可能是在与恋人的“特殊性行为”过程中，仅仅由于男方采用的骑跨胸腹部体位这个单一的因素所导致。既然如此，法官们在分析死亡原因时，当然也必须考虑鉴定人明确指出的“潜在病理改变”了。<br />
&nbsp; &nbsp; 如果仅仅以最高人民法院的这份鉴定结论为审判依据，法官们也只能做出“被告姜俊武的行为与被害人黄静的潜在病理改变是造成死亡的共同原因”的推断。因为在这份鉴定文书中，与死亡原因有关的因素除了压在黄静胸部的姜俊武身体的部分重量外，就是黄静身体中所谓的“潜在病理改变”，更因为鉴定人已经明确“排除因机械性损伤、口鼻及颈部压迫所致机械性窒息、常见毒物所致中毒以及电击、高低温等方式直接导致的死亡”。<br />
&nbsp; &nbsp; 事实上，除了压在黄静胸腹部的那部分重量外，这份鉴定文书已经把所有的暴力因素全都排除在外了，这样一来，黄静案就走进了一个死胡同：没有哪个法官会相信一个大活人会被另一个大活人活活地压死，而且这个压力仅仅只限于特殊性活动中的骑跨胸腹部体位。因此，黄静的死因除了压在胸腹部的那个力量外，一定还同时伴有一个联合死因。这个联合死因究竟是什么？既然法医鉴定已经明确排除了其他暴力因素，当然就不可能是暴力，既然后三次鉴定的专家已经完全排除了前三次公安机关认定的冠心病、风心病以及肺梗死，当然也就不是致死性疾病。最后，这个联合致死因素只剩下一个可能，那就是确确实实是一种或几种连法医自己都说不清楚的“潜在病理改变”。<br />
&nbsp; &nbsp; 当然，如果真能排除所有的暴力因素，这样的可能也不是完全没有，但是，前提必须是真能排除。<br />
&nbsp; &nbsp; 我个人认为，导致黄静死亡的联合死因可以排除机械性损伤、可以排除常见毒物所致中毒以及电击、高低温，但却绝不能够轻易排除口鼻及颈部压迫所致机械性窒息。<br />
&nbsp; &nbsp; 首先，黄静的急性肺水肿符合机械性窒息死的尸体征象，其次，单一的胸腹部受压难以形成导致黄静死亡的急性肺水肿，而在胸腹部受压的基础上，再用手捂压口鼻或扼压颈部，也就是用外力控制住黄静的胸腹部后，再用外力控制住黄静的头颈部，这个身体健康的女孩则有可能完全失去反抗能力，而在极短时间内形成致命性肺水肿。<br />
那么，在黄静的尸体上，是否可以排除捂压口鼻及扼压颈部的行为呢？让我们一起来看看：<br />
第一次公安机关于发现死亡当日进行的尸体检验，除了双下肢的皮肤抓痕外，丝毫没有记载头面部、颈项部及上肢的任何损伤痕迹。如果这些部位真是没有一点损伤痕迹，黄静的死亡到真是有些奇特了，这样的奇特用科学的观点是解释不通的。试想，一个身体健康的人，谁能够让别人压在自己的胸腹部，一动不动地直到把自己活活地压成致死性肺水肿，除非这个人的上肢或者颈部或者头面部被外力紧紧地控制住。既然是外力，既然是紧紧地控制住，就有可能留下与尸体下肢相类似的痕迹。在这样的情况下，即使颈部体表没有留下明显的痕迹，也完全有可能通过解剖检验发现损伤证据。遗憾的是，第一次检验根本就没有对颈部进行必要的解剖，试问，没有切开颈部的皮肤，又怎么可以排除扼压颈部导致的机械性窒息呢？难道这不是一个法医学常识吗？<br />
第二次公安机关于死亡20余天进行的检验发现：“头面部于左下颌角后1公分处见一皮下红点，表皮无破损，双侧颈肩结合区皮肤色红，其中右侧见一针尖大小表皮破损，颈后部颈背结合区正中第六颈椎处见一1.30.4公分表皮剥脱，色红。”遗憾的是，鉴定人认为，这些损伤“均无明显生活反应，说明系死后形成”。<br />
看到这里，我感到很恐怖，鉴定人居然用肉眼就敢判断表皮剥脱有无生活反应，就敢因为“无明显生活反应”而断定系死后形成。我就不明白了，既然死者家属提出这些部位很可能是生前扼压颈部形成的损伤，为什么不按常规解剖检验颈部的皮肤、皮下及肌肉组织呢？不对颈部进行解剖检验，又怎么可以排除扼压颈部导致的机械性窒息呢？<br />
第三次公安机关于死亡3个多月之后再次对尸体进行了重新检验，这次由公安部法医参加的检验，仍然没有对颈部进行常规的解剖。因此，这次的检验依然不能排除扼压颈部导致的机械性窒息。<br />
令人匪夷所思的是，在尸体保存完好的情况下，在尸体明显呈现急性肺水肿和严重缺氧征象的情况下，在没有对颈部进行常规解剖的情况下，公安机关的法医居然一连三次做出了后来被所有专家证明是完全错误的因病致死结论。<br />
这样的结论真是令人恐怖！<br />
&nbsp; &nbsp; 第四次法医鉴定仅就前三次公安机关的鉴定资料进行书证审查，根据书证审查，明确提出：“黄静属非正常死亡，因风心、冠心病或肺梗死猝死的根据不足”。<br />
&nbsp; &nbsp; 参加第五次鉴定的中山大学法医专家于黄静死亡5个多月的2003年8月2号对尸体进行了第四次解剖检验。尸体外表检验明确记载：“尸体呈高度腐败状，全身皮肤可见腐败性表皮剥脱及腐败气泡，霉菌斑形成。眼窝下陷，鼻塌陷，容貌不可辨认。”<br />
&nbsp; &nbsp; 此次解剖与公安机关的第三次解剖仅仅相隔不足两月，尸体竟然变得如此面目全非，不得不令人扼腕长叹。显然，公安机关第二次鉴定时认定的那几处所谓的“死后表皮剥脱”，已经随着腐败的形成而难以查明真相了。<br />
&nbsp; &nbsp; 面对这具高度腐败的尸体，中山医科大学的专家们没有根据公安机关的“发现”和“记载”，贸然排除捂压口鼻腔和扼压颈部导致机械性窒息的可能性，而是留下了这样的文字：“本次检验，由于尸体已高度腐败，有关的尸表皮肤、皮下软组织损伤、尸体内部器官改变已无法进行准确的判断和鉴定”。<br />
&nbsp; &nbsp; 即使在这样的情况下，专家们依然没有放弃对颈部的解剖检验，专家们对颈部解剖检验做了如下记载：“颈部皮肤腐败，颈部浅深肌群可辨认。”<br />
&nbsp; &nbsp; 很明显，通过颈部解剖检验，专家们既没有肯定但也绝没有否定颈部的皮肤以及皮下组织有无出血。我个人认为，这样的态度是客观的科学的负责任的，也是很有意义的。因为“颈部皮肤腐败，颈部浅深肌群可辨认”的客观描述，告诉我们了一个事实，那就是通过对腐败尸体颈部的解剖检验，无法排除颈部皮肤及皮下软组织的出血！<br />
&nbsp; &nbsp; 我想提请大家注意的是，在中山大学的司法鉴定书中，多次出现“皮肤及皮下软组织未见明显出血”的描述，如“背部皮肤及皮下软组织未见明显出血”、“腹壁未见出血”、“双侧肩部皮下软组织未见明显出血”。如此明确的意在排除损伤的文字描述，却偏偏没有出现在最应该被排除的颈部，动机和用意不言自明。<br />
&nbsp; &nbsp; 中山大学的法医鉴定对黄静案作出的最大贡献应该是首次提取了喉，并对喉部进行了组织病理检验，做出了可疑出血的病理诊断。这一科学的检验活动，再次警示和告诫我们，绝不能贸然排除捂压口鼻腔和扼压颈部导致机械性窒息死的可能性！<br />
&nbsp; &nbsp; 2004年4月2号，“由于缺乏死因鉴定的主要材料（心脏等器官），且保存的尸体已经腐败”，司法部的法医专家终止了对此案的第六次鉴定。<br />
&nbsp; &nbsp; 2004年6月28号，也就是中山大学法医专家对尸体进行检验的10个月之后，最高人民法院组织全国法医专家对黄静的尸体进行了第五次解剖检验。这样一个“迟到”的解剖检验，在对尸体颈部进行了“颈部皮肤、皮下及肌肉组织未见异常”，对喉的组织切片进行了“表皮被复鳞状上皮自溶明显，结构不能辨认，骨骼肌横纹可辨，但细胞核已消失，肌束间可见团状组织，由残存淋巴细胞和结构模糊的细胞轮廓（无法确认为红细胞）所组成”的描述之后，却又断然作出“排除口鼻及颈部压迫所致机械性窒息”的分析说明。<br />
&nbsp; &nbsp; 恕我直言，这样的“排除”不仅轻率而且毫无道理甚至有些霸道。面对一个高度腐败的尸体，任何一个法医都不能以自己“未见异常”而排除异常，不能以自己“无法确认出血”而认定不是出血！<br />
&nbsp; &nbsp; 这个案件折腾到现在这个地步，谁都没有回天之力让黄静的尸体重新回到腐败前。惟有初检法医无意中留给我们的那张能够反映喉部颜色变色区的照片，以及中山大学法医专家为我们制作的反映喉部可疑出血的病理切片，还在“挣扎”着为死者鸣冤叫屈。<br />
&nbsp; &nbsp; 其实，这两个证据并不孤立，黄静死于急性肺水肿是个不争的事实，这个致死性的急性肺水肿绝不是压在胸腹部的压力能够单独完成的，既然黄静体内不存在导致急性肺水肿的病理基础，就只能考虑其他暴力参与了急性肺水肿形成的过程，而其他暴力最有可能的就是捂压口鼻和扼压颈部。<br />
&nbsp; &nbsp; 立足尸体、重视现场、参考案情、彼此印证。<br />
&nbsp; &nbsp; 如果把这16个字切切实实地运用到黄静案的分析中，特别不要忘记加黑的文字，正确的答案就一定会出现在我们每一个职业法医自己的心中！</div>

							
							
							
							
