其实，一个女孩子，让自己的男朋友背着走走并不是什么奇怪的事情，但是，从始至终一直趴在别人的背上，并不是一件很好玩很舒服的事情，况且，这期间已经都到了自己的学校，一个瞌睡至极的女孩子为何不赶紧回家睡觉，却只是因为男朋友的车子不能停，而要舍近求远地陪着男朋友绕一个大圈子，再让男朋友背着走200多米后才打着车回家，难道让男朋友背着走200多米再爬7层的楼梯，是一件很开心的事情吗？<br />
一个既没有醉酒，又没有致昏的女孩子，像个死人似地被人放在车后座，像个死人似地被人背来背去背到了自己的宿舍，在后来静悄悄流过的几个小时之内却又偏偏被人发现平静地死于急性肺水肿。我相信，只要是个有正常思维的人，都会对来自于死者母亲的质疑表示深深的理解。然而，经过这个母亲历时多年的苦苦申诉，经过国内多次的法医鉴定，最终的结果却被法庭认定为这个女孩子死于恋人间特殊性行为中的猝死。<br />
试问，假如你是个母亲，假如这样的事件就发生在你的身边你的家庭，你会欣然接受，还是会保持沉默？再试问，假如你是个职业法医，你会不会和我一样，高度怀疑黄静的宿舍并非死亡第一现场，而是移尸现场？<br />
<br />
二，黄静的死亡时间距尸体外表初步检验并非仅为３―４小时。<br />
按照“被怀疑对象”的说法，黄静在他离开的时候还活着，如果真是如此，那么，初检法医对尸体进行的尸表检验结果，尸体温度应该不低于３２度，尸斑应该开始出现但指压肯定不退色，尸僵应该还没有完全形成，死亡时间应距此次检验约３―４小时。<br />
第一次法医检验对死亡时间的尸体体表征象描述及分析，初看起来，似乎完全验证了“被怀疑对象”的说法，那就是“触之有明显温热感，尸斑呈暗红色，分布于颈后项部，腰背及四肢背侧未受压部位，压之不退色，尸僵未形成，认定其死亡时间应距第一次检验约３―４小时。<br />
但是，只要用法医的脑子仔细地分析一下便不难看出，这样的描述存在两个不容忽视的问题：<br />
第一，“触之有明显温热感”，含有太多主观感受，这样的描述也很容易诱导人们相信被怀疑对象的说法。根据尸温推断死亡时间是法医学的基本常识，科学的方法是测量尸体肛门的温度，相信所有公安机关的法医对这样一个基本常识和方法都不会陌生。假如我们的初检法医在事发当天为死者黄静测量了尸体温度，并且真实地记录下这个数据，那么，我们今天的所有讨论就变得毫无意义了。<br />
第二，每一个职业法医都应该知道，尸斑与尸僵的形成及发展是遵循特有的规律性按部就班地进行的。在尸僵完全未形成的情况下，尸斑却已经“呈暗红色，并且广泛“分布于颈后项部，腰背及四肢背侧未受压部位”，这样的尸体现象显然不能用正常的死后变化来解释。如此违背自然规律的文字描述不得不让人高度怀疑，事实上，初检法医在对尸体进行体表检验时，并非尸僵未形成而是尸僵已经形成。由于尸僵是否形成只是凭法医的手感进行认定，所以，有没有手感只能凭法医的良心来说话，而那些已经成为暗红色并广泛分布于全身低下部位未受压处的尸斑，就不像尸僵那么好糊弄人了，按照规定，这些尸斑必须作为技术证据以照片的形式被固定和保留下来，这样一来，有真有假的记载就会让非常明显的漏洞和矛盾跃然纸上，以至于不得不令人深思了！<br />
让我们一起来看看几乎是同一时间来自于当地市中心医院１２０医生记录的原始证据：“全身皮肤淤斑，已死亡多时，死亡查因。”<br />
上述事实，让我们完全有理由高度怀疑，黄静的死亡时间距尸体外表初步检验并非仅为３―４小时！<br />
<br />
三，黄静并非死于特殊性行为中的猝死，而是死于强奸过程中的机械性窒息<br />
导致黄静死亡的真实原因究竟是什么？要回答这个问题，就必须从尸体上的证据说起。还是文章一开头的那句原话，根据笔者审阅黄静母亲当面递交的先后六次法医鉴定文书，不难看出，尽管后三次的法医专家已经否定了前三次公安机关认定的冠心病、风心病、肺梗死的死因鉴定，但是，有一个事实却是所有参与检验并得出不同结论的法医全都不得不承认的，那就是在黄静的尸体上真正能够让我们清清楚楚看到的导致死者死亡的直接死因只有一个――急性肺水肿！<br />
现在需要讨论的是引起黄静急性肺水肿的原因究竟是什么？换句话说，也就是导致黄静死亡的根本原因究竟是什么？<br />
能够在短时间内引起致命性的肺水肿，无外乎疾病与暴力。如果黄静的心脏确实潜在致死性的病变，或者黄静的肺动脉确实存在致死性的梗塞，那么，我们今天的探讨就实在有些乏味和无聊。但是，如果经过国内诸多著名法医病理学家反反复复地复查，彻底否定了上述病变，那么，在黄静的体内就根本不存在能够引起急性肺水肿的任何潜在病变，那么，引起黄静急性肺水肿的原因，就只可能是暴力因素！<br />
什么样的暴力能够在短时间内导致发生在黄静身上的致命性肺水肿，答案是肯定的，窒息。而在此案中，最为可能的窒息方式，就是机械性窒息。<br />
没有任何来自于尸体的证据，可以证明黄静是在与恋人进行性游戏的过程中诱发潜在病变导致猝死；集中在尸体双下肢的损伤高度提示黄静生前曾遭遇强奸的暴力袭击；千真万确出现在黄静尸体上的致命性肺水肿明明白白地告诉世人，在排除了所有可能导致黄静发生急性肺水肿的因素外，机械性窒息就成了唯一可以解释导致肺水肿发生的致死性因素。<br />
有鉴于此，我们完全有理由高度怀疑：黄静并非死于特殊性行为中的猝死，而是死于强奸过程中的机械性窒息！<br />
<br />
四，导致黄静机械性窒息的暴力行为并非仅为胸部的压迫，而是呼吸道受阻与胸部受压并举<br />
我并不认同黄静的母亲及某些病理专家关于黄静死于胸部受压所致机械性窒息的说法，我认为这样的说法甚至比特殊性行为诱发潜在病变导致的猝死更加荒唐。道理很简单，一个鲜活的生命，不可能被另一个同类活活地压死，除非这个生命的某个重要器官存在着某些潜在的能够在死亡过程中起到重要作用的病变。<br />
黄静以急性肺水肿的尸体语言告诉我们，她生前所受到的暴力绝不仅仅如“被怀疑对象”所自述的那样，只是在特殊性行为过程中用自己身体的部分重量压在了她的胸部，因为单纯这样的压力是不足以导致死亡的，除非黄静真是个有着某种直接参与死亡过程的潜在疾病的病人。<br />
假如排除了潜在疾病，那么，导致黄静急性肺水肿死亡的联合因素就剩下一种可能了，那就是胸部的压迫与暴力导致的呼吸道堵塞并举。<br />
