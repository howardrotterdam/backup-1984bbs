													<h2>山东日照现役军人遭党支部书记暴力袭击</h2>
						
						
																											<div id="postmessage_625472" class="t_msgfont"><a href="http://rmsdw.com/article/show.asp?id=1302" target="_blank">http://rmsdw.com/article/show.asp?id=1302</a><br />
发布日期：2010-03-17 13:33:19&nbsp;&nbsp;来源：维权万里行&nbsp;&nbsp;将本页收藏至：QQ书签&nbsp;&nbsp;百度收藏<br />
&nbsp; &nbsp;&nbsp;&nbsp;在隶属于解放军总装备部某部服役的山东日照籍士官小庄，今年春节回老家探亲，3月16日到本村村委了解自家楼房被政府强行拆除的有关情况时，遭到村党支部书记徐西田的无端殴打，被送往当地医院紧急抢救。目击事发全过程的北京某报记者李京、张常安在讲述这一事实的时候用“惊心动魄”四字来形容。 <br />
<br />
&nbsp; &nbsp;2008年4月1日，山东省日照经济开发区奎山街道办事处大岭三村村民庄光合全家外出有事，奎山街道办事处会同大岭三村村委会，在大批公安、城管等部门的助威下，砸烂他家的门锁，强行搬出楼房内的酒店用品，动用两台铲车，霎时间就将他的两层楼房夷为平地。来不及拆卸和笨重的物品，如空调、冰柜、座椅等都被砸在废墟中，价值20多万的物品、昔日美好的一个家就这样被毁了。<br />
<br />
&nbsp; &nbsp; 为了维护自己的合法权益，两年多来，庄光合跑遍了日照所有的职能部门，却没有任何反响。今年春节后，在总装备部某部服役的儿子小庄回家探亲，看到昔日美好的家园已经变成了一条宽阔的马路，家已经无影无踪了。在租住的房子里，小庄知道了自家楼房被强拆的经过。2010年3月16日，小庄和父亲庄光合在北京某报记者李京、张常安的陪同下来到日照市军分区，力图通过正常途径解决家里的事情，尽快返回部队工作，但被告知军分区8位领导皆外出开会。在日照市委宣传部，新闻科张科长要求小庄逐级反映。下午三点，小庄和父亲等来到大岭三村村委会，见到了村支书徐西田。在和小庄父子交谈中，小庄父子提到当年大岭三村旧村改造政策公决书中有些条款不和情理时，村书记徐西田百般狡辩，声称当时拆房是有村里全体党员的签名才进行的，房主不签字照样拆。小庄就大岭三村旧村改造政策公决书的合法性和村支书徐西田辩论起来，徐西田言语中口口声声称庄光合的房子不是村中的宅基地，不在改造范围，你要回迁楼，就要花钱购买。<br />
<br />
&nbsp; &nbsp; 在场的北京某报记者李京、张常安介绍说，当时，双方在争论中话不投机，徐西田抡起自己的钢化水杯，砸向坐在他对面的小庄。小庄在防卫中站起身来，却再次被该书记推倒在墙角的花盆上，手被破烂的花盆扎伤。此时，该书记手持钢化水杯，发疯似地再次砸向小庄的头部，顿时小庄血流满面。在厮打中，村支书的左眼被撞到肿起。失去理智的徐西田又抡起大木椅，砸向已经抽身向外走的小庄，被庄光合夺下，庄光合左手臂也被刮伤。这时，小庄身穿的绿色军装上的纽扣和胸前的徽章被鲜血染红。闻声而来的群众把二人拉开，小庄被迅速送进了日照市中医院急救中心。<br />
<br />
&nbsp; &nbsp; 在急救中心，记者了解到，医院在对小庄做了CT检验后，对其头部、耳部和右手伤口进行了清创缝合。接警后的110民警来到急救中心了解情况。<br />
<br />
&nbsp; &nbsp; 据了解，2008年4月1日，山东省日照经济开发区奎山街道办事处大岭三村强行拆除村民庄光合合法房产后，庄光合为维护自己的合法权益，多次到日照市各级部门讨要说法，有关媒体也积极进行舆论监督。《山东法制报》先后两次发表该报记者采写的文章，人民网、光明网、中国企业信息网、中国维权万里行、新农村商报等全国主流平面和网络媒体纷纷进行报道。但是，强有力的舆论支持并没有引起日照市有关部门的足够重视，没有认识到违法强拆的严重性，上演本次“袭军门”足见他们违法乱纪已至极致。对此，我们将继续关注。（记者 子秋）<br />
