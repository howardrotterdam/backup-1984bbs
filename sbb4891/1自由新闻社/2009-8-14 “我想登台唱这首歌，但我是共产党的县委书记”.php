____________________________________________<br />
<br />
<strong>张北县，一个极普通、极普通的国家级贫困县，第一次被人关注是1998年初，发生了6.2级地震；第二次是2004年除夕，Hu Jintao来张北村民家包饺子。<br />
第三次，摇滚来到张北，三天时间，全世界知道了这个小县城。“心连心”没有做到的事，摇滚做到了。</strong><br />
摇滚一不留神成了一个县脱贫，甚至致富的希望……<br />
<br />
<strong>“看，那就是朋克！”李雪荣坐在指挥车里，指着窗外两位年轻人的冲天鸡冠头，兴奋地对南方周末记者说。</strong><br />
李雪荣是中共张北县委书记，他所管辖的张北县位于河北省西北部，是国家级贫困县。2009年8月9日傍晚，在张北县城外的一块1500亩的草地上，为期三天的“张北草原音乐节”进行到了最后一天。<br />
李雪荣和县委、县政府各级领导干部进行现场指挥，嘈杂的电子舞曲不时传进车内。一年前，李雪荣和他的同事们根本不知道什么是朋克，也没想到他们有一天会用摇滚来推动一个贫困县的脱贫，甚至致富。<br />
<br />
<br />
共产党人的摇滚观<br />
<br />
“张北草原音乐节”是从张北县委副书记孙小涵开始的。<br />
1990年代初，孙小涵在大学时代听到了崔健，让他印象深刻的是《一无所有》。当时，他有些纳闷，“这个人唱歌怎么和我的嗓子差不多。”在他的脑海里，摇滚乐等于小痞子、小流氓和小混混。<br />
来张北县之前，孙小涵在河北省委宣传部负责出版工作，结识了石家庄一本摇滚乐杂志的主编，这位主编有空就给孙小涵进行摇滚扫盲。长年累月之后，孙小涵渐渐明白，在西方，摇滚乐是一种为大众广泛接受的文化，它不是洪水猛兽，也不是小痞子、小流氓、小混混的专属品。<br />
孙小涵成了张北县委、县政府里对摇滚乐了解最多的领导干部，他也渐渐听说有一种东西叫“摇滚音乐节”。<br />
<br />
“去去去，你给我整点实惠的。”一个北京投资音乐媒体的朋友向李雪荣提议合作搞摇滚音乐节，被李雪荣“打”了回去，他从张家口调到张北一年多，一门心思想帮贫困的张北搞点“项目”，摇滚、音乐这种“不实惠”的东西，显然不是他心中的“项目”，这位有多年党龄的共产党员对摇滚乐并不了解，上学时曾看过一些这种西方文化的资料，当时的认识停留在“资本主义腐朽文化”的层面上，他根本不会想到摇滚音乐节和国家级贫困县之间能扯上任何关系。<br />
张北物资不算丰富，有些矿产和蔬菜资源，风力发电也是张北的一个特产。张北县第一次被国人广泛关注是1998年初发生的6.2级的张北地震。第二次是2004年除夕，Hu Jintao到张北看望农民，在村民家包饺子。<br />
<br />
孙小涵曾就如何宣传张北做过许多尝试，甚至动过请张艺谋制作“印象系列”的想法，来一个“印象张北”，可最终还是因为“太贵”没搞成。<br />
<br />
<strong>“只要内容健康，只要不反对共产党，为什么不可以让大家享受摇滚乐？”张北县委书记李雪荣说自己的想法很简单，“不开放，张北就没有出路”。</strong><br />
<br />
<br />
他们还做过一台“张北心连心”文艺晚会，一个月前，这台晚会在后来举行音乐节的同一块草地上演出，李双江、尚雯婕等艺术家、明星来到张北，现场涌入了两万多当地观众，甚至挤坏了围起的栅栏。张北人第一次看到了电视里的大明星。<br />
<br />
但这些都没有让人记住张北，也没有给张北带来“实惠的项目”。<br />
<br />
李雪荣那个北京朋友锲而不舍，拿来了国外知名摇滚音乐节的资料及海报，李雪荣隐隐觉得，对张北而言，没准是一次打造新名片的尝试。于是，他告诉县委副书记孙小涵：“可以考察一下。”<br />
孙小涵开始熟悉“伍德斯托克”、“迷笛”这些名词，他看了许多国外摇滚音乐节的资料，考察出一些音乐节的确是在偏远小镇举行，经年累月，取得了巨大成功，小镇因此名声在外。由此，他得出结论――这确实是一个“项目”，一个可以让全国人民认识张北、了解张北的好项目。<br />
<br />
摇滚音乐节的想法一经提出，在县委、县政府并未获得一致认可，<strong>毕竟，与“心连心”相比，它太不同寻常了。</strong><br />
“只要内容健康，只要不反对共产党，为什么不可以让大家享受摇滚乐？”李雪荣说自己的想法很简单，“我的思想是很开放，不开放，张北就没有出路。”<br />
<br />
但李雪荣同时提了一个要求――对参加音乐节的乐队和艺人及其作品要进行严格筛选，在政治上把好关。<br />
<br />
音乐节的筹备是从6月开始的，前后只有短短一个多月。按照协议，张北县负责水电、通讯、租场地、修栅栏、厕所等一系列基础设施的建设。<br />
8月7日，为期三天的“张北草原音乐节”拉开帷幕，来自大陆、台湾、瑞典、英国的八十多个乐队及艺人参加了这次音乐节，涵盖了摇滚、朋克、电子、民谣等多种音乐风格。<br />
1500亩的草地按照国际摇滚音乐节惯例，被划分为演出区及生活区、主舞台、服务台及电子舞台，让音乐通宵达旦地响彻张北的夜空。<br />
据音乐节主办方统计的不完全结果显示，大约有10万人次参加了此次音乐节，其中以第二天8月8日最多，这一天是周六，观众人数达到了五六万。<strong>（我觉得这数纯扯淡）</strong><br />
<br />
“宇宙人都知道了张北”<br />
“我就跟他们组织者说，应该让Tricky星期六演，<strong>他们非要让他星期天压轴，那些白领星期一都是要上班的。</strong>”孙小涵坐在车里，面对车窗外背包离开的“朋克们”，惋惜地说。<br />
面积四千多平方公里的张北从来没有接待过这么多人，张北县领导们倾了全县之力来保证音乐节平安进行。<strong>“这么多人来张北，我们甚至想到了热比娅，想到疆独藏独会不会袭击这里。”孙小涵说。</strong><br />
张北县的日常警力只有三百多名警察及民兵。为了确保音乐节期间的安全，他们发动了当地的机关干部、教师组成临时安全保障小组。为了严防现场可能出现的肢体冲突，按照<strong>国际惯例</strong>，经与主办方协商，他们严禁携带任何瓶装饮料入场。<br />
就在张北筹备自己的音乐节时，加拿大和中国内蒙古的音乐节分别出了事――加拿大某音乐节的舞台坍塌造成一人死亡，七人受伤；内蒙古某音乐节，由于组织混乱，发生了观众将酒瓶砸向舞台等一系列事件。李雪荣特地安排人将这些音乐节事故的资料整理出来，与大家逐一讨论，希望从中找到可供参考的前车之鉴。<br />
<br />
他们也拿出了自己的“反面例子”：一个多月前，“心连心”艺术团来这里演出，当地群众没见过那么多大明星，一激动就把围栏冲垮了。<br />
<br />
<strong>“这次我们提前把场打开，放摇滚音乐，花花绿绿的让老百姓免费来看，看了一两天，他们知道是怎么回事了，音乐节正式开始的时候，他们就该卖鸡蛋的卖鸡蛋，该卖包子的卖包子了。”</strong>李雪荣说。<br />
幸好，只有喜欢音乐的人“袭击”这里，崔健、高晓松这些明星，甚至“上面的大领导”也都悄悄来看了演出――因为“大领导”的孩子喜欢摇滚乐。<br />
<br />
音乐节让张北成为媒体的焦点，国内各大报纸及网站都在显要位置对“张北草原音乐节”进行了报道。香港、法国、波兰等地的媒体也派出记者到现场进行了采访。<br />
<br />
李雪荣怎么也想不到自己几个月前的那句“可以尝试一下”，就“尝试”出这么大动静。他的一位朋友在音乐节第二天给他发来短信：“连宇宙人都知道张北了。”<br />
<br />
“真是值了。”李雪荣说，张北县与音乐节北京主办方签的协议是十年，他们希望把它做成一个响当当的品牌。当然，音乐节背后，他希望为张北打出的是旅游牌。<br />
“我们不能老在穷字上躺着。”李雪荣强调。<br />
<br />
<br />
<br />
“从北京来了这么多车，真好”<br />
<br />
在一个国家级贫困县举办摇滚音乐节，对于当地农民而言，既是一次商机，也是一次可能改变命运的切身体验。<br />
音乐节第一天，当地农民看到那么多人涌入张北，就提着篮子，到场地周围卖起了鸡蛋和矿泉水。为了能让农民增加一些收入，县委、县政府领导没有在音乐节期间实行限价。<br />
张北县城的一家宾馆旁边有一个小卖部，步行仅需五分钟。宾馆里住着参加音乐节的媒体人士、乐队及艺人，深更半夜，演出结束，这些人只能到小卖部买些零食充饥。音乐节三天，小卖部天天都是昼夜营业，这是此前从未有过的现象。小卖部的老板说，音乐节三天的收入会比他们平时的月收入高出几倍。<br />
来自张北县的数据显示，这个国家级贫困县2008年的人均年收入是2700元，而这一年国家公布的农民人均年收入是4700元。<br />
<br />
音乐节的空前客流让一些当地农民把自己的家变成了临时小旅社。一些前往张北参加音乐节的城市里的年轻人因此体会了一个中国贫困县农民家庭的真实生活――屋子很昏暗，有点脏，但这些农民大都很热情，把过年才吃得起的莜面拿出来给客人吃。<br />
“张北草原音乐节”三天，没有多少草的草地上出现的是这样的场面――视野辽阔，草地上支着许多帐篷，帐篷周围聚集着一大批身穿“奇装异服”的年轻人。他们有的在抖空竹，有的席地而坐喝着啤酒。不远处的三个巨大舞台，传来巨大的声浪，年轻的观众们聚集在舞台周围，随着音乐一起摇摆，他们发出尖叫，互相碰撞着彼此的身体，他们把Pogo（摇滚演出现场中观众随着音乐节奏一起互相碰撞身体，借此表达友好）带到了张北。<br />
<strong>（我PO了两年，从来不是以表达友好为目的……）</strong><br />
<br />
显然，还是有一些张北当地的年轻人被这种气氛吸引了。<br />
<br />
新蜂音乐创始人付8月6日随同舞台制作人员提前一天抵达张北。许巍试音那天，一位骑着摩托车溜进现场的张北年轻人对付说：<strong>“我想明天来看，但我没有票。万一混不进来，我就开着滑翔机来，在天上看。”<br />
这位张北年轻人做旅游生意，有供游客俯瞰草原景色的滑翔机。</strong><br />
<br />
在第二天看朋克乐队Subs演出时，付和身旁一位张北农民交谈起来。看着舞台上歇斯底里的表演，这位张北人带着浓重的口音大声喊着：“好！够卖力气。”<br />
<br />
在付看来，这位叫好的农民也许不知道朋克是什么，他只是用表演者是否卖力气、是否认真来判断音乐的好坏，“但这又有什么关系呢？也许，就是在这些第一次看摇滚现场的张北人中，可能会出现十年后赞助草原音乐节的人。”付说。<br />
<br />
在Pogo的人群里，一些张北年轻人试着和大家一起随着音乐节奏互相冲撞，他们的服装、肤色、口音十分容易辨认，他们开始有些胆怯，跃跃欲试的样子，但很快就和人群撞在一起了。<br />
<br />
生于1967年代的张帆被认为是中国摇滚音乐节开创者，今年是他创办的迷笛音乐节的第十年。张北三天，张帆认识了许多当地人。让他难以忘记的是一位十几岁的张北小姑娘对她妈妈说的一句话：“妈妈你看，从北京来了这么多车，真好！”<br />
<br />
8月7日晚，“痛苦的信仰”乐队登台演唱《一直往南开》，观众们自发组成火车长龙，一个人的手搭住另一个人的肩膀，随着节奏一起向前跳跃，在投向观众区的灯光照耀下，可以看出其中有很多张北的年轻人，火车越接越长，在人群中不断穿梭，就像一辆真的火车行驶在草地上。<br />
<strong>这个画面让张帆很感动，他对南方周末记者说：“对于许多张北人来说，这可能是他们一生中不可磨灭的记忆，甚至可能会改变一些人一生的命运。贫困县的年轻人不该被边缘化，他们应该得到重视、得到快乐的生活。”</strong><br />
<br />
事实上，这些挤进音乐节现场跟着摇滚节奏一起舞动的，还有县委书记李雪荣。他对舞台上的轰鸣与嘶吼并不排斥，演出第一天许巍上场时，他被沸腾的观众感染了，他站到最前面，跟着音乐节奏摇动身体。“这时候，我忽然看到摄像机的镜头冲着我来了，赶紧停了下来。”李雪荣说。“张北草原音乐节”的主题歌也是摇滚风格，歌名叫《欢迎来到草原》，这首歌其实有两个版本，另一个版本的名字叫《欢迎来到张北》，演唱者正是李雪荣。<br />
<br />
主办方曾经邀请李雪荣在音乐节期间与摇滚乐队们一起登台演唱这首歌，<strong>“我其实挺想替张北登台唱这首歌的，但一想自己毕竟是共产党的县委书记，还是算了。”</strong>李雪荣说。<br />
<br />
上台唱歌容易，但想把音乐节当成新名片却不那么容易，在张帆看来，音乐节期间暴露出的交通、住宿等基础设施的问题亟待改善。“要想吸引大城市来的消费者，他们要做的还有很多。”张帆建议当地政府在音乐节结束后，立即着手植被的恢复工作，否则裸露的土壤在强烈的西北风下，会迅速沙化。<br />
<br />
<br />
<br />
泥浆、雨伞和厕所<br />
<br />
“我一直怕下雨，他们跟我说，就想在泥浆里打滚。”县委书记李雪荣并不太清楚伍德斯托克的标志――泥浆，他听说来参加“张北草原音乐节”的观众期望那三天能下一场大雨，那样，大家就可以在泥浆里摇滚了。李雪荣至今还是没太能想象出那是一种什么场面，所以还是让县里准备了一些雨伞和帐篷。<br />
<br />
今年8月是伍德斯托克音乐节40周年，“伍德斯托克是无法复制的，它是一个乌托邦，它只属于1960年代。”张帆说。<br />
<br />
从2001年起，张帆曾多次参加在国外的摇滚音乐节，他认为那个年轻人举起反叛旗帜的年代，一去不复返了。伍德斯托克的泥浆也不复存在了。但伍德斯托克的基因依然会得到传承：自由、平等、博爱。<br />
<br />
实际上，大雨对“张北草原音乐节”会是一场噩梦，由于担心张北的风很大，更容易造成舞台坍塌，这次音乐节的舞台没有架设防雨顶棚。音乐节的三天，任何一场大雨都将导致演出的中止。幸运的是，三天里，张北一直没怎么下雨。<br />
<br />
三天的“张北草原音乐节”让李雪荣总结出了“幸福指数”这个指标，他看到来看音乐节的人在用另一种方式享受着生活，他们很幸福；但那些表面“幸福”的人却并不“幸福”。<br />
音乐节结束后，没能在张北体会到幸福的观众在网上发表了质疑与批评，这些负面评价主要集中在交通、食宿、草地、物价等几个方面。<br />
<br />
有十年操办音乐节经验的张帆同时是“张北草原音乐节”的顾问，他强调参加这类野外音乐节，观众需要有充分的“吃苦受罪”的思想准备，并预备好御寒的户外帐篷、防潮垫、睡袋、酒精炉等户外野营用品。他提起自己参加欧洲一些音乐节的“遭遇”――如果没能赶上雨天，音乐节场地肯定会是漫天尘土（几万双脚早把植被踏没了）；如果赶上了雨天，在泥浆里翻滚之后，就得在阴冷的帐篷里待着，脚泡在雨水里。<br />
<br />
“这也是摇滚音乐节的另一种乐趣。”张帆说。<br />
<br />
“这地方得有人气，没人气可不行。”李雪荣一个月来一直关注网络，他让专人随时针对网友的评论，对场地、设施和问题进行调整、处理。<br />
<br />
音乐节最后一天傍晚，李雪荣用五个字对它做出了自己的评价――“不尽如人意”。由于罕见的干旱使得草地上的草非常稀薄，形成了演出现场的黄土漫天飞扬，音乐节第二天尤为突出。他也听到许多乐迷将这次“草原音乐节”戏称为“黄土音乐节”，李雪荣明确表示明年会与主办方协商，在草地上种草，并对其进行必要的维护。他对现场厕所太少、太远给观众带来的不便深表不安：“由于没有经验，厕所成了大问题。”<br />
<br />
“明年，明年肯定要多修几个厕所。”孙小涵在去厕所的路上，指点着几个未来厕所的地方，对于张北这个国家级贫困县而言，他们的确把脱贫的希望，寄托在了十年摇滚音乐节上。</div>

							
							
							
							
