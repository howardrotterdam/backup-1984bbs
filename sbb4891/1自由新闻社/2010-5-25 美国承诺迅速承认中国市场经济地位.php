													<h2>欧盟称未考虑承认中国市场经济地位    我朝学者表示反对</h2>
						
						
																											<div id="postmessage_644491" class="t_msgfont">欧洲议会主席称未考虑承认中国市场经济地位<br />
<br />
2010年05月28日08:22<br />
<br />
&nbsp; &nbsp;【专题・聚焦】第二轮中美战略与经济对话 | 美方将尽快承认中国市场经济地位<br />
<br />
　　中国日报网消息 英文《中国日报》5月28日报道:正在北京访问的欧洲议会主席布泽克（Jerzy Buzek）星期四在接受中国日报专访时表示欧盟仍未考虑承认中国的市场经济地位。<br />
　　布泽克作出这番表态之时,正值美国表示将承认中国的市场经济地位后两天。本月25日,当中美第二轮战略与经济对话在北京闭幕的时候,美方表示将通过中美商贸联委会,以一种合作的方式迅速认可中国市场经济地位。<br />
　　而布泽克在采访时则说中国还没有“达到欧盟的认定市场经济的标准”,即中国还没有 “施行（implement）”一些法律法规。<br />
<br />
　　他在说这番话后,便立刻提及到于1976年生效的联合国《公民权利与政治权利国际公约》。该公约的序言说:“本公约缔约各国,考虑到按照联合国宪章所宣布的原则,对人类家庭所有成员的固有尊严及其平等的和不移的权利的承认,乃是世界自由、正义与和平的基础。”<br />
　　中国政府虽然于1998年签署了该公约,但并没有在国内执行它。因为联合国政治权利公约属于国际法,中国一旦批准这个公约后,就会面临着作为国际法的国际人权公约与中国国内法的关系问题。<br />
<br />
　　而布泽克则在采访中呼吁中国尽快批准并执行该公约。<br />
<br />
　　对此番言论,中国学者认为将中国的市场经济地位与该公约挂钩是站不住脚的。<br />
　　中国政法大学欧盟法专家张彤认为欧盟对市场经济的判断虽然有自己的评判标准,“但不一定合理”。<br />
　　张彤介绍到,欧盟对判定市场经济颁布了五个标准,简单地说包括企业自主权,政府干预程度,劳资谈判自由度,汇率兑换自由度等。<br />
　　事实上2001年,国内学者李晓西曾经按照美国的市场经济判定标准对中国做过市场化测评,最后得出的数字是中国的市场化程度已经达到69%,可以说已经取得了初步市场化的成果。2005年,再次做同样的测评师,这个数字达到78%。<br />
<br />
　　张彤说在同样标准下欧盟曾对俄罗斯和中国做过测评,按照测评结果,俄罗斯的市场化程度低于中国。然而,2002年6月美国承认了俄罗斯的市场经济地位,紧接着,同年欧盟便也承认了俄罗斯的市场经济地位。“这里面恐怕还是受到西方所谓'行动一致’的影响。”<br />
<br />
　　《欧洲联盟基础条约政策》里规定了成员国对基本价值的认同和推广责任。不光是经济方面,也在政治条件。欧盟认可有序竞争的市场经济,公正自由的司法裁决体系,以及民主自由的政治环境。<br />
　　对于不承认中国市场经济地位,张彤认为欧盟其实“是在用自己的价值观来衡量其他国家,并希望其他国家也能够认同并推广他们的价值观”。<br />
<br />
　　欧盟讨论设置“西藏特使”<br />
<br />
　　除了不承认中国市场经济地位之外,布泽克主席在采访中也向记者坦言中国和欧盟之间还存在诸多问题和分歧,特别是在“少数民族权利与公民权利”方面。<br />
　　例如,西藏便是布泽克、乃至整个欧洲议会眼中中国的“少数民族问题”。他还向记者透露,欧盟议会内部正在讨论设立“西藏问题特使”。<br />
　　他认为这是欧盟内一个“期待(expectation)”。<br />
　　欧洲议会干涉中国西藏问题的历史由来已久。<br />
　　从欧洲议会档案显示,从2000年开始,该议会每年都至少会做出一次涉藏决议。谋求设立西藏问题特使也不是什么新鲜事。2008年4月10日, 议会通过决议,要求欧盟理事会任命一名西藏问题特使,以“协助有关各方的对话和谈判”。<br />
　　其实除了西藏问题,欧洲议会在过去几年也屡屡在人权方面上触犯中国的利益。在欧盟三大机构（同时包括欧盟理事会和欧洲委员会）中,曾经有媒体将欧洲议会称作“最反华”的机构。<br />
　　可是布泽克对这种说话断然否认。“完全不对。我们并不反华,”布泽克说,并称其此次访华的主要目的就是在全球危机和挑战日益增强的时候加强欧盟和中国的合作。只是“少数民族问题在我们的观念中非常重要”,他如是说,并称欧盟尊重中国的主权和领土完整。<br />
<br />
　　对于设立所谓的“西藏特使”的说法,中国人民大学的欧洲问题专家房乐宪认为中国政府“不能答应”,并且要“保持警惕”。<br />
　　房乐宪说中国应该通过各种渠道告诉欧洲,西藏关乎中国内政,是中国的核心利益,不能“国际化”。<br />
　　同时,他还说如果欧盟设立西藏问题特使,还有可能产生连锁反应,使更多的西方国家模仿。<br />
<br />
