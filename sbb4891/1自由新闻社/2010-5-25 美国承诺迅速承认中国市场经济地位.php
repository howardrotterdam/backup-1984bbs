中美对话成果丰硕 美将迅速承认中国市场经济地位<br />
<br />
<a href="http://news.ifeng.com/mainland/special/zhanlueyujingjiduihua/content-1/detail_2010_05/25/1553216_0.shtml" target="_blank">http://news.ifeng.com/mainland/s ... /25/1553216_0.shtml</a><br />
<br />
2010年05月25日 21:38中国新闻网【大 中 小】 【打印】 共有评论74条<br />
<br />
<br />
5月25日，第二轮中美战略与经济对话在北京结束。美国总统奥巴马的特别代表、国务卿希拉里・克林顿、财政部长盖特纳同中国国家主席胡锦涛的特别代表、国务院副总理王岐山和国务委员戴秉国出席了总结对话成果的联合记者会。 中新社发 盛佳鹏 摄<br />
<br />
中新社北京5月25日电(记者 张蔚然)第二轮中美战略与经济对话25日在北京发布成果清单。战略对话取得26项具体成果，经济对话达成五方面重要内容。<br />
<br />
战略对话取得26项具体成果。<br />
<br />
中美双方签署了《中国国家核安全局和美国核管制委员会关于进一步加强西屋AP1000核反应堆核安全合作备忘录》、签署了《中国国家能源局与美国国务院中美页岩气资源工作组工作计划》、签署了《中国国家发展改革委与美国国务院关于绿色合作伙伴计划框架实施的谅解备忘录》，宣布成立绿色合作伙伴计划联合秘书处，并启动新一轮的绿色合作伙伴结对工作。<br />
<br />
中美签署了《中华人民共和国海关总署与美国国土安全部海关与边境保护局关于供应链安全与便利合作的谅解备忘录》、续签了《中华人民共和国卫生部和美利坚合众国卫生与公共服务部关于新发和再发传染病合作项目的谅解备忘录》。欢迎在落实《中美关于加强气候变化、能源和环境合作的谅解备忘录》和《中美能源和环境十年合作框架》方面取得的进展，并同意加强在气候变化、能源、环境等领域的务实合作。<br />
<br />
双方发表了《中美能源安全合作联合声明》。双方同意加强在阻止、打击核及其他相关放射性材料非法运输方面的合作。同意在平等和相互尊重的基础上继续就人权问题进行对话，期待明年在中国举行下一轮人权对话。作为《联合国气候变化框架公约》的缔约方，重申对《哥本哈根协议》的支持；宣布中国国家发展改革委和美国环保局将履行对2009年11月《关于加强应对气候变化能力建设合作备忘录》的承诺，并拟在一个月之内举行会议启动实施；同意就短寿命温室气体启动讨论。<br />
<br />
经济对话成果包括五方面重要内容。<br />
<br />
――双方承诺进一步加快转变经济发展方式、调整经济结构。美方承诺将推动建立更加平衡和可持续的经济发展模式，提高储蓄率。中方将继续提高国内消费对经济发展的贡献率，加强社会保障体系建设。<br />
<br />
――双方承诺致力于构建更加开放的全球贸易和投资体系，反对贸易和投资保护主义。美方将在贸易救济调查中，认真考虑并给予提出“市场导向行业”申请的中国企业公正、合理的待遇，并通过中美商贸联委会，以一种合作的方式迅速承认中国市场经济地位。中方对依法在华经营的包括美国企业在内的外资企业提供国民待遇。<br />
<br />
――双方同意加强金融领域的交流与合作，充分发挥金融机构支持实体经济的功能。美方欢迎外国资本投资于美金融业，承诺对中资银行、证券和基金管理公司适用与其他国家相同的审慎监管标准。<br />
<br />
――双方同意加强在国际金融体系改革方面的合作。双方同意共同努力，确保多伦多、首尔峰会取得积极成果。<br />
<br />
――双方鼓励通过两国部门间经济对话机制深化双边经济合作与交流。包括通过中美科技联委会进一步讨论创新问题等。<br />
