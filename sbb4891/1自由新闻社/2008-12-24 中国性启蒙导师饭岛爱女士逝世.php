													<h2>幽门螺杆菌小常识</h2>
						
						
																											<div id="postmessage_13936" class="t_msgfont">1983年，澳大利亚两位科学家，从慢性胃炎的胃粘膜中取样，在微需氧的条件下，培养出幽门螺杆菌（Hp），并指出这种菌与慢性胃炎的直接关系以来，引起了全世界医学界人们的广泛研究和证实，并在活动性慢性胃炎及消化性溃疡病灶中，查出幽门螺杆菌，检出率为98%和100%。此菌被公认为慢性胃炎及消化性溃疡的致病菌。<br />
<br />
幽门螺杆菌的致病机理： <br />
<br />
幽门螺杆菌的致病机理目前还不十分清楚。受幽门螺杆菌感染的人有的发病，有的不发病，发病也各不同；有的人发生慢性胃炎，而有的人发生消化性溃疡。有关研究还在进行之中。<br />
<br />
幽门螺杆菌的传播途径：<br />
<br />
一般认为幽门螺杆菌仅寄居于人类，人是唯一的传染源。幽门螺杆菌多系口－口传染，因为在牙菌斑中可以培养出幽门螺杆菌。而粪－口传染途径尚未得到证实。<br />
<br />
知道了幽门螺杆菌是怎样相互传染的，大家就应该预防为主，讲究个人卫生，常洗手，勤刷牙，勿食被污染的食品，实行分餐制等等。<br />
<br />
幽门螺杆菌相关性胃病：<br />
<br />
下面介绍几种与幽门螺杆菌感染最为密切的几种疾病：<br />
<br />
1. 慢性胃炎<br />
<br />
慢性胃炎发病率很高。有上腹不适患者的检出率可达80％以上，但过去对其病因却一直不清楚。自从澳大利亚两位科学家发现幽门螺杆菌以后，对幽门螺杆菌的研究成为全世界医学研究的热点。研究证实幽门螺杆菌是慢性胃炎的主要病因。我国也于1985年首次分离出幽门螺杆菌，并对幽门螺杆菌进行了大量的基础和临床研究，发现慢性活动性胃炎病人中幽门螺杆菌感染率为95％，幽门螺杆菌阳性的胃炎多为活动性胃炎，杀灭幽门螺杆菌后则变为非活动性胃炎。慢性活动性浅表性胃炎逐渐发展可以转变为慢性萎缩性胃炎，继而加重萎缩性胃炎并发生肠上皮化生及异型增生，成为癌前病变。萎缩性胃炎被认为是胃的癌前疾病，因此萎缩性胃炎患者每1－2年需胃镜复查一次，以便及时发现早期癌变。<br />
<br />
2. 消化性溃疡<br />
<br />
消化性溃疡比较常见，其胃镜检出率为16.5％－28.9％。过去认为“无酸（pH)则无溃疡”，抑酸可愈合溃疡。虽然抑酸愈合溃疡不难，但一年内复发率高达60％－90％。幽门螺杆菌的发现和相关研究显示，消化性溃疡与幽门螺杆菌的感染密切相关。我国胃溃疡的幽门螺杆菌检出率约为70％，十二指肠溃疡的幽门螺杆菌检出率约为90％，而根除幽门螺杆菌之后经过长期随访观察，溃疡复发率明显下降至10％以下。因此有人提出了“无Hp（幽门螺杆菌）则无溃疡”的说法。<br />
<br />
3. 胃癌<br />
<br />
根据流行病学资料，幽门螺杆菌与胃癌的发生有十分密切的关系，幽门螺杆菌被认为是胃癌的一个高危致病因素。实验研究显示幽门螺杆菌可引起细胞过度增殖，使DNA易受损伤；幽门螺杆菌还可引起原癌基因激活，抑癌基因失活，癌基因过度表达及基因突变等因此认为幽门螺杆菌是胃癌的一个启动因子。</div>

							
							
							
							
