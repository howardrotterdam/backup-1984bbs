													<h2>深圳大亚湾核电站发生核泄漏事故</h2>
						
						
																											<div id="postmessage_682764" class="t_msgfont">2010-06-14<br />
<br />
深圳大亚湾核电站发生历来最严重的核泄漏事故，本台获得独家消息指，核电厂一个机组在上月23日运作出现异常，检测发现幅射泄漏超出厂区范围，至今仍未受到控制，严重威胁附近居民性命安全。当局因为担心引起恐慌及打击当地房地产，一直封锁消息，亦未有依据协议向香港方面汇报。国务院总理温家宝得悉事件后亲自致电核电厂，要求如实反映情况。核电厂的安全谘询委员会成员包括多名香港学者及中电代表，对泄漏事故感到震惊。（李莉报道）<br />
<br />
核泄漏事故发生在上月23日中午，当时大亚湾核电站内正在运转的2号机组突然出现异常，工作人员立即采取紧急事故处理程式。本台由香港专家消息人士得悉，电厂的检测系统发现，当时已有大量放射性碘核素散布于空气，空气中的活性气体也大量增加，显示幅射泄漏已超出厂区范围。电站高层得知事件后要求保密，只作内部处理。直至下午，电站才依据国家要求，向北京有关部门作简单通报，并称情况得到控制。<br />
<br />
据悉出事的机组已运转10多年，这次泄漏怀疑与金属疲劳有关，事实上电站已有老化迹象，单在5月内曾发生4宗异常事故，包括2次停电及1次回水电路故障。<br />
<br />
这次泄漏事故发生后，当局采取大事化小处理，消息人士指，核电厂不仅没有跟据有关协议向香港方面通报，连对国务院亦只是轻描淡写简报。原因是深圳大亚湾附近已发展成豪宅及观光旅游景区，当局担心泄漏消息公布，会造成恐慌，严重打击当地房地产。<br />
<br />
上周四大亚湾核电站举行的一年一度安全谘询委员会，厂方在会议报告中首次向成员汇报泄漏事故，与会包括多名大陆及香港的专家学者，及合作方香港中电的代表。记者透过渠道得到该份会议报告，报告称核电站今年“上半年发生了几则异常事件”o初步分析有“一根燃料组件存在微小核泄漏”，已采取加强监测及幅射防护措施，及减少堆芯扰动操作等补救措施。<br />
<br />
消息人士表示，与会者对发生泄漏事故均感到十分震惊，并察觉事态严重，虽然再三追问，但厂方仍强调情况得到控制。有专家曾提出停产检修，但厂方担心正值大陆用电量高峰期，一旦停产，每天将损失近200万元的电费收入，因此迟迟未有处理事故，以至泄漏至今仍未受到控制。<br />
<br />
据专家介绍，核燃料设有燃料棒、压力罩及混凝土三重密封式保护，这次发生泄漏的组件正是最内层的保护层燃料棒，原先一直使出法国出产的燃料棒，两年前开始转用大陆制的军用燃料棒，曾被各方质疑其安全性。<br />
<br />
消息人士又引述厂方表示，国务院总理温家宝得知事件后亲自致电电站高层，询问有关情况，并要求如实向中央反映，对事故高度重视，不能掉以轻心。为了求证此事，记者向安谘会主席、港方专家立法会议员何锺泰查询，其助理表示他因工务繁忙，拒绝接受访问。<br />
<br />
记者致电大亚湾核电站，一名姓陈的安全技术员对记者表示，没有听闻有发生泄漏，电站目前正常运作，亦无特别为员工加强防护装备。他h：从来没有听说有个泄漏，我们在核电站里也不知道。<br />
<br />
<br />
记者问：现在电厂都运作正常吗？<br />
<br />
回答：正常的。<br />
<br />
记者问：员工没有什么特别发生吗？<br />
<br />
回答：没有发生过什么事情，都在正常上班。<br />
<br />
<br />
记者致电大亚湾核电站母公司中国广东核电合营公司，但大陆正值假期，无法联络上公司高层。而占有一成多股份的中国核电北京总公司，值班人员指大亚湾核电站并非公司管辖，对事件不知情。<br />
<br />
大亚湾核电站位于深圳龙岗区大鹏半岛，是大陆建成的第二座核电站，也是大陆首座使用国外技术及资金建设的核电厂，其后当局在核电站侧建设岭澳核电站，组成大型核电基地。<br />
