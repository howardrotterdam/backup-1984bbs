大鹏镇居民对核电站事故毫不知情<br />
2010-06-14<br />
<br />
深圳大亚湾核电站位于龙岗区大鹏镇，筹备兴建时曾引起争议及反对，区内居民更担心当局会隐瞒辐射泄漏事故。有生化学专家警告，泄漏辐射会对居民及环境造成严重的影响。（姬励思报道）<br />
<br />
大亚湾核电站位于大鹏镇岭澳村，镇内一名居民对本台粤语组表示，未听闻上月底核电站发生泄漏辐射事件，亦未察觉区内出现异常情况。居民已习惯与核电站并存，大部份人都已不太关心安全的问题。她说：我在这里这么久，未听说过发生事故，一般而言，无太多人关心这方面的问题，很少有人讨论。<br />
<br />
但另一名村民担心，核电厂一但发生严重事故，当局会隐瞒。但作为普通市民，她亦感到很无奈，因为他们没有任何途径可了解到核电站的情况。她说：我们只是能偶尔在网上简单的看一下，其他就无甚么途径让我们了解这方面的事情。<br />
<br />
由于香港邻近深圳，香港天文台因应香港社会对大亚湾核电站安全问题的关注，设立了一个环境辐射水平的监察系统。天文台发言人马伟民表示，该系统有十个监察站，24小时检测区内的环境辐射水平，一但到达危险的标准，就会发出警报。他说，最近一个监察系统距离大亚湾核电站约20公里。他说：香港有十个监察站，量度香港的辐射水平，系统24小时运作，我们会密切留意该水平，有事时警报会响。<br />
<br />
马伟民说， 自大亚湾核电站开始运作至今，他们未有检测到有泄漏辐射的问题。<br />
<br />
中国政府于上世纪八十年代决定兴建大亚湾核电厂时，引起香港社会广泛的关注，担心核电厂发生事故时，会波及香港。随著核电厂建成并开始运作后，香港社会对核电厂的安全问题似乎续渐遗忘。<br />
<br />
不过，香港中文大学生化系系主任邵鹏柱表示，核电厂的安全问题不容忽视，一但发生辐射泄漏事故，对居民的健康有严重影响，甚至祸延下一代。他说：严重的话，居民会短时间内死亡，长远而言，对他们的身体如患癌症等，对他们的后代亦有影响，会产生变异，例如无手无脚，或智力受损。<br />
<br />
邵鹏柱又说，对生态环境的影响更是难以弥补。地区将长时间无法居住，因为这些放射性物质很久都不会消退，残留在泥土，水里，可能长达几千年。<br />
