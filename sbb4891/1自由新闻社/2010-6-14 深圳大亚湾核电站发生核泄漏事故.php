													<h2>RFA: 居民担忧发展核电事故危险</h2>
						
						
																											<div id="postmessage_682866" class="t_msgfont">2010-06-14<br />
<br />
中国近年积极发展核能供电，未来15年每年平均兴建4座核电站，成为全球之冠，令国际原子能总署也深表关注。对于这次大亚湾核电站核漏事故，有民众表示不清楚，他们明白核漏发生的重大后果，但认为基层民众亦没有选择的馀地。（林静报道）<br />
<br />
<img src="http://i49.tinypic.com/1zjedu.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
浙江杭州附近的秦山核电站的核反应堆。（法新社2005年图片） <br />
<br />
目前中国有四个地区的核电站共11台机组在运作，包括江苏田湾核电站、浙江秦山核电站以及这次涉嫌核漏的深圳大亚湾核电站及岭澳核电站。对于这次大亚湾核漏事件，对居住在其他核电站附近的民众，会不会造成恐慌。<br />
<br />
居住在浙江省秦山核电站附近的李先生认为，这次只是个别事件，相信政府会作出适当处理，即使当地有大型核电厂亦表示不担心。他说：那边离我们有点远，我们没有听说过的。我知道放射性这东西，但我想没事吧，政府会处理好的了。<br />
<br />
早前有报道指，中国未来十年将以超乎寻常的速度发展核电，消息指新建核电厂数量将是世界其他国家总和的三倍。中国核电厂数量猛增，令外界对其安全问题产生顾虑，特别是中国很多核电设施建造在大城市附近，一旦发生事故，可能会使千百万人暴露在辐射之下。<br />
<br />
报章引述指，国际原子能总署（ＩＡＥＡ）核设施安全部部长贾梅表示，人才短缺是中国扩大发展核电的一大隐忧，中国是否有足够训练的工作人员来满足核设施迅速增加的需要成一大疑问。<br />
<br />
中国现时兴建中核电站的选址，包括辽宁省红沿河、福建省宁德及福清、广东岭澳二期、广东阳江及浙江省三门等。<br />
<br />
<img src="http://i48.tinypic.com/vebr87.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
秦山核电站内景。（法新社2005年图片）<br />
<br />
当中在广东省江门市的台山核电厂，将会在2013 年年底初步投入服务，当地居民赵先生表示，不少当地民众对项目仍然不甚了解，大家都担心核能发电会释放有害物质。如今大亚湾核电站发生核漏事故正好提醒大众，核能发展所产生的重大后果，但他表示即使问题发生，基层民众亦没有选择的馀地。他说：我没有听过这事情，这兴建的事情我们那有权提出反对不反对，有钱人也不担心的话，我们贫穷的人更加不用说担心的了。<br />
<br />
另外，广东河源亦正规划兴建核电站，有当地民众表示曾经反对兴建核电厂，但政府却决意兴建，对于大亚湾核电站发生核漏事件，他表示非常讶异，但认为即使日后当地核电厂兴建后发生同样问题，政府亦会隐瞒事件。他说：也没有办法了住在这里不到你想不想，我们老百姓有有何能力阻止政府呢，我们也想搬走但政府不肯。即使知道知道后果，但也没有办法。没有地方安置所有受影响的人。<br />
<br />
根据中国《核电中长期发展规划（2005―2020年）》，表示鉴于煤碳资源的紧缺和环境保护的需要，中国将大力发展核能供电，对核电站的开发和建设加大力度，未来15年内，将以平均每年兴建2至4座核电站。据了解，中国目前的核电仅占总电力的1.8%，到2020年将上升到4%以上。这意味著在未来15年中，中国新增核发电量将达4000万千瓦。扣除已建成和在建的870万千瓦，还将兴建31台百万千瓦级核电机组。<br />
<br />
按照中国政府的计划，在 2020年之前，核能和可再生能源如水以及风力发电将覆盖中国四分之一的能源供应市场。<br />
