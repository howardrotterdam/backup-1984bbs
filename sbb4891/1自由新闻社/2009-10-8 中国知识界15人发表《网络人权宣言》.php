&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;中国知识界15人――包括作家、学者和律师，2009年10月8日在互联网上公开发表《网络人权宣言》，全文如下――&nbsp;&nbsp;<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; <strong>《网络人权宣言》</strong><br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 站在人类历史长河的高岸上，我们将会看得更清楚：互联网这一新技术革命带来的社会变化和进步，对扩展中国人的自由与基本人权，无异于远古时代的铁与火、犁与轮对文明进程的影响。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 我们注意到，网络公民传播的时代不可阻挡地来临。公民记者通过手机、数码相机拍摄事件，传播事实真相，已经成为时尚和新潮；网络也给公民提供出无限创意空间，通过博客、播客、网络论坛甚至是跟贴等途径，表达意见。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 我们认为，关心公共事务是公民的责任，而关注网络言论自由也是网络公民的责任。网络公民合法地发表言论、报道真相，是行使公民权利，也是促进这个古老文明注入以个人幸福与基本人权为核心价值的新血液，促进全体国民的福祉，因之应该予以鼓励、善待和宽容。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 有鉴于此，我们认为如下理念是值得肯定的：<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 1，互联网言论自由是公民言论自由的一部分，是人类的最基本的人权和最美好的价值之一，值得追求、珍惜、爱护。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 2，每个网络公民（NETIZAN）在网络上合乎当地宪法原则和法律的发言，用文字、声音、图画、视频表达意见，应该受到保护、鼓励。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 3，发表权是网络公民最基本的权利，此项发表权尤其体现在个人博客、播客，体现在论坛中。发表权不应受到超出法律范围之外的审查和干扰。持有与发表意见应有免于恐惧的自由和权利。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 4，编辑权应予以尊重；此项权利不应受到法律范围之外其他权力的困扰。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 5，采访报道权是网络公民应有的权利之一；此项权利应受宪法的言论自由原则保护；公民行使此项权利时应该尽可能重现事实真相，避免歪曲、捏造和恶意诽谤。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 6，评论权和交换意见的权利是网络公民应有的权利之一，此项权利应包含有质疑和监督的权利，含有批评和抵制的权利。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 7，匿名表达权是公民言论自由的一部分，匿名是为了作者更便利地表达意见。只要匿名作者合乎宪法和法律地表达意见，此种合法权利应予以尊重。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 8，网络资讯搜索权是公民表达权、知情权、监督权的一部分。一方面，合法的网页资讯不应予以屏蔽；另一方面，对事关公共领域的个人资讯的搜索权，应予以尊重和保护。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 9，网络隐私权应予以尊重和保护。网络公民的真实身份与个人资讯，除非经公正透明的司法程序和被证明为必要，不得被网站予以公开。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 10，合乎宪法和法律的网络信息自由流通权，应该予以尊重和保护。对违背言论自由原则的网站审查、屏蔽、封锁，应该被舆论谴责，并通过司法诉讼等程序寻求言论自由的正义。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;我们呼吁，为使公民重视网络言论自由对全体国民人权与幸福的意义，应该设立网络人权日。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 鉴于1911年10月10日，我们的先贤和志士浴血而起，终结了一个残暴的种族歧视王朝和漫长的帝制形式，在亚洲大地上首创了共和政体。为了追忆他们自由的热血和勇气，我们提议：每年10月10日，为中国网络人权日。<br />
