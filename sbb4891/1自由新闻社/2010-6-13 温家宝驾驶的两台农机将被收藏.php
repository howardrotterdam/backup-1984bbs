													<h2>温家宝驾驶的两台农机将被收藏</h2>
						
						
																											<div id="postmessage_679414" class="t_msgfont"><a href="http://news.163.com/10/0613/12/692F3BMD000146BD.html" target="_blank">http://news.163.com/10/0613/12/692F3BMD000146BD.html</a><br />
<br />
“我的驾驶技术还行吗？”“很好！很好！”当温家宝总理跳下东方红拖拉机的驾驶座，四周响起一片热烈的掌声。<br />
<br />
6月10日，在许昌的田间地头，实地察看小麦收割情况时，温家宝总理兴致勃发，先是登上一辆农用收割机，不顾驾驶室里的闷热，与农机手“联合”在金色麦浪间收割了500米的小麦。随后，温总理又坐上一辆正在播种的东方红拖拉机，在农机手的示范下，亲自驾驶拖拉机行进了200米，将黄澄澄的玉米种子播进了丰收的沃土 昨天上午，记者赶赴许昌，追访两位农机手，听他们深情讲述与温家宝总理的亲密接触。<br />
<br />
“我伸手将总理拉进驾驶室”<br />
<br />
6月12日，河南许昌县陈曹乡，金色的麦浪翻滚，成行的白杨树将一望无垠的麦田分成整齐的方块。<br />
<br />
记者边走边打听，上午10时许，摸进了后孙汪村村支书张铁根的家里。<br />
<br />
一位中等身材的河北农家汉子，站在记者面前嘿嘿地笑着，张铁根介绍说：“这就是教总理开收割机的吕素彬。”<br />
<br />
说起与温总理的亲密接触，吕素彬有些激动，不停地搓着双手。<br />
<br />
吕素彬说，他老家在河北石家庄市赵县前大章乡乡官村，今年44岁，从10年前开始，就一直天南地北地奔波，在河南河北沿线收麦子。“有人叫我们麦客。2007年，我和俩老乡花了7.6万元，买了这台收割机，年年都到后孙汪村来收麦子。”<br />
<br />
“我是一个农民，能和总理一起开一会儿收割机，那是我一生中最大的荣幸。”吕素彬一边回忆两天前的情景，一边向记者描述着当时的情况。<br />
<br />
“事先没有任何人通知我，说总理要上我的收割机，一切都太突然了。”吕素彬说，当时临近中午，他驾驶着收割机，正在后孙汪村村东头路南的一块麦田里收麦子，刚刚开到田边，就见一群人向他走来。其中，走在前面的人冲着他亲切地微笑着。<br />
<br />
“我仔细一看，哎呀，这不是我们的总理嘛。”吕素彬说，当他认出收割机前站着的是温家宝总理时，心里一阵激动，但没有任何紧张。“总理太平易近人了，让人感觉不到一点紧张。”<br />
<br />
正当他拉开收割机的驾驶门，准备下来时，温总理摇手让他莫下来，并告诉他，温总理要上他的收割机。<br />
<br />
“登上驾驶室的铁梯有四五级，在驾驶室的左门边。可温总理拉开右门，总理脚蹬着收割机又高又大的轮胎，要进驾驶室。我当时啥也没想，伸出手就拉了总理一把，下边的人扶着，总理才上到了3米多高的驾驶室。”<br />
<br />
“驾驶室里面又闷又热，当时我浑身都汗透了。一看总理坐进来了，我第一个反应是打开头顶上的电风扇，把风扇开到了最大。可是没啥用，不一会儿，我就见总理头上出汗了。”<br />
<br />
总理边学驾驶边拉家常，没一点架子<br />
<br />
“我长这么大没见过县长以上的大官，这次猛不丁地遇见总理，还跟他坐在一个又闷又热的驾驶室里，心里直觉得过意不去。可温总理不顾得这些，他先问我好，问我是什么地方人，年纪多大，子女成家没有。在拉完这些家常后，又问我柴油供应紧张不？购买农机的补贴有没有到位？”吕素彬说，在他一一回答后，总理让他开着收割机收麦子。于是他驾驶着收割机，从地头到地尾跑了个来回。其间，总理又问他，收割机有几个挡位，咋驾驶。<br />
<br />
“我给总理讲，我的收割机共有4个挡位，3个是前进挡，1个是倒退挡。总理就接着问我，哪个是倒退挡。在走过一个来回后，总理坐到了驾驶座上，我把挡位摘到了空挡上，看着总理打火，我又帮着总理把主离合器拽了起来。”<br />
<br />
吕素彬说，可能总理担心不会收割抛洒了麦粒，当时并没有开着收割机走，只是坐在驾驶座上感受了一会儿。然后，总理拉开左侧的门，沿着铁梯子下到了地面。<br />
<br />
“我也立即下了驾驶室，总理伸手要与我握手，我赶紧摘了手套，与总理握手。总理还对我说了一些勉励的话。”吕素彬说，现在想起当时的情景，还颇为感慨，作为一国总理，对农民这么亲切，没一点架子，实在让人感动。<br />
<br />
“没想到一个国家总理，对农民这么好。”听了吕素彬的话，在一边旁听的村支书爱人师秀花忍不住接过了话头。<br />
<br />
她说，2009年初，温总理就曾来过后孙汪村，指导这里的抗旱。总理对老百姓很亲切，让人一点儿也不紧张。“收麦子时身上到处是灰，就是戴着手套，摘掉手套手上也是灰，可总理非要和大家握手。”<br />
<br />
“驾驶室的温度有四五十摄氏度，当时总理身上都汗透了。现在想起来，总理能亲自上来体验我的工作，不嫌脏不嫌热，这样的国家领导真是很难得。”吕素彬说。<br />
<br />
总理虽然是第一次开拖拉机，但开得很稳<br />
<br />
12日下午2时，在许昌豪丰机械装备有限公司会议室里，36岁的农机手程召阳正向来自全省的媒体记者讲述总理开拖拉机的经过。<br />
<br />
程召阳说，他是许昌县张潘镇孟庄村的，以前在南方打工，去年才回来，在豪丰农机装备有限公司上班，具体工作就是试车新产品。<br />
<br />
程召阳说，6月10日，他驾驶着东方红LX 950拖拉机，后面挂着公司生产的播种机，在后孙汪村的麦田里作业。“前边收割机收小麦，后边我们就对收割后的麦田进行松土、施肥和播种，播下秋玉米种子。”<br />
<br />
“温总理从收割机上下来后，恰好我开着拖拉机也过来了，总理招呼我停下来。”程召阳说，他从拖拉机上下来后，总理坐上了拖拉机的驾驶位。随后，他也上了拖拉机，教总理开拖拉机。<br />
<br />
“总理在驾驶座上，先问我是不是踩这个？我明白总理的意思，他是想问我，右边的两个踏板，哪个是油门。我就告诉总理，哪个是油门哪个是刹车。问清楚后，总理随即就用脚松开了刹车，点在了油门上。”<br />
<br />
“我给总理挂的是低速一挡。这种型号的拖拉机，共有12个挡位，因为当时刚下过雨，麦田有些湿，如果挂了高挡，拖拉机会很吃力。”程召阳说，考虑到总理初开拖拉机，担心犁具下得太深开起来吃力，他就用液压操纵杆把犁具稍微向上提升了一点。<br />
<br />
程召阳感叹地说，虽然是第一次开拖拉机，可温总理很老练，挂挡、加油门，紧握方向盘，稳稳地驾驶着。在刚刚收割的200米长的麦地里深耕、施肥，种下了秋玉米。<br />
<br />
<strong>两台农机将被一拖集团收藏</strong><br />
<br />
采访中，记者得知，温总理先后登上的两台农机，均是中国一拖集团生产的东方红系列产品。<br />
<br />
“我买的收割机是东方红4LZ 2.5型。11日下午，一拖集团的领导已经来见过我了，想把我的这台收割机运到他们的展览馆里收藏。”吕素彬说，一拖集团领导承诺，将于12日晚上把一台最新款的多功能联合收割机赠送给他。新的联合收割机，不仅能收小麦，还能收割玉米、油菜等多种农作物。<br />
<br />
程召阳驾驶的东方红拖拉机，是今年4月份新买的。<br />
<br />
河南豪丰农机装备有限公司得知一拖集团的想法后，对这台新拖拉机依依不舍。<br />
<br />
一拖集团党委书记、工会主席任惠娟正在许昌，与吕素彬和豪丰公司商量，将以何种方式收藏温总理乘过的东方红农机。<br />
<br />
她说，一拖集团已和省委省政府沟通过了，省委省政府同意由生产厂家对这两台东方红农机进行陈列展览。<br />
<br />
“从总理的言谈举止中，我们农民感受到了国家对农业农民的重视”<br />
<br />
除了两台东方红农机成了有纪念意义的机械，位于许昌县陈曹乡后孙汪村的国家综合农业开发区，也成了记者频频造访之地。<br />
<br />
张铁根说，因为偏远，后孙汪村以前啥都不是。划为国家综合农业开发区后，已在农业专家的指导下，做到了树成行、田成方，路林沟渠统一规划。虽然土地是个人承包，但实行统一机耕、机耙、机播、机收，以及统一病虫害防治。“现在的后孙汪村，不仅是许昌的10万亩高产示范区，也是农业部万亩高产示范区。去年和今年的亩产，均能达到550公斤以上。”<br />
<br />
“总理这次视察小麦收割，并亲自体验农机收割和播种，表明了国家对农业机械化的关注。”虽然是农民，吕素彬和程召阳也善于“总结”。<br />
<br />
他们说，从总理亲民的言谈举止中，不但感受到了总理极其崇高的人格魅力，也让他们了解了国家领导人关心农民、关注农业、关注农业机械化的“政策方向”。晚报记者 熊堰秋<br />
(本文来源：中国新闻网 )</div>

							
							
							
							
