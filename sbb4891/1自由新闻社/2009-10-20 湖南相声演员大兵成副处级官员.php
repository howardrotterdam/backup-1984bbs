																											<div id="postmessage_291778" class="t_msgfont">湖南相声演员大兵成副处级官员<br />
 2009-10-20 <br />
&nbsp; &nbsp;湖南省文化厅昨天（19日）正式引进大兵进入湖南省话剧团，担任副团长一职，行政级别为副处级。大兵坦言，“这辈子，从来没想过自己会成为‘国家干部’！”大兵上任后将筹备组建国内首个喜剧团。<br />
　　《潇湘晨报》报道，“讲相声，我对你的口味……”眯着“小眼睛”一脸坏笑的大兵，有着太多的经典段子。现在，除笑星、主持人外，大兵又多了一个称号，“大兵副团长”。<br />
<br />
　　昨天，湖南省文化厅正式引进大兵进入湖南省话剧团，担任副团长一职，行政级别为副处级。眯缝着招牌“小眼睛”，笑呵呵地大兵坦言，“这辈子，从来没想过自己会成为‘国家干部’”。他说，自己当官是为了做一个话剧艺术的召集人，让喜剧表演者不再“散落在民间，天天打游击”。<br />
<br />
　　着手组建湖南喜剧团<br />
<br />
　　作为中国南派相声的领军人物之一，大兵可谓家喻户晓。“自己是一个自由惯了的艺术家，这辈子做梦都没有想到有一天会当上国家干部。这说明湖南省文化厅不拘一格选拔录取人才，我也很高兴被看中并委以重任。”在昨天的“湖南省文化厅人才培养与引进工作新闻发布会”上，大兵坦言“十分荣幸获任话剧团副团长”。<br />
<br />
　　大兵说，自己新官上任之后，将搭建一个更权威、更高的官方艺术平台，对湖南曲艺艺术进行“集约化”发展，让喜剧表演者不再“散落在民间，天天打游击”。他还幽默地说，“自己当官不会去天天想着管理某人，而是做一个话剧艺术的召集人而已，希望剧团的艺术家们团结在一起，为了共同的艺术理想而奋斗。”<br />
<br />
　　据悉，经湖南省文化厅研究批准，大兵将加快拓展省话剧团发展空间，筹备组建国内首个喜剧团――湖南省喜剧团，届时将创作和生产更多、更好的艺术精品。</div>

							
							
							
							
