　　“跨越－2009”的大规模军事演习中的我军装甲部队。刘逢安 摄<br />
<br />
<br />
<br />
<br />
<br />
　　俄罗斯《独立报》10月9日文章，原题：数百万士兵加上现代化武器　 从近期中国军事建设的特点可以清楚地看出，中国未来扩张并非是以和平（经济和人口）的方式，不排除其动用武力的可能性。<br />
<br />
　　最近一系列事件又让我们重拾这一话题。最显著的是中国庆祝建国60周年举行大型阅兵。据官方数字，阅兵式上中国展出了50种新型技术装备，人民解放军各兵种也参加了检阅。可以说，北京展示自己实力是成功的，这次阅兵式无可挑剔。不过，（正如）俄罗斯在5月举行的阅兵式上并未充分展示俄军队的真实现状，因此，人们对中国军队中没参加阅兵的那部分力量更感兴趣。<br />
<br />
　　不久前中国刚结束“跨越－2009”的大规模军事演习。令人关注的是，此次演习并非明显针对台湾的登陆作战和击退美国的入侵。因为台湾面积不大，无需数千公里的行军。而美国如果入侵中国，会利用海上和空中力量打击，不会在陆上采取行动，且奥巴马政府已表示尽量避免在海外动用美军。中国也不会遭到来自某个国家的侵略，因为其周边国家的军事潜力都落后于它。<br />
<br />
　　这就出现了这样一个问题，中国准备使用最现代化的军事技术装备同哪国军队作战？中国并不打算向东南亚和朝鲜半岛扩张，而是准备向俄罗斯和哈萨克斯坦扩展。只有在这些战区能发动纵深达2000公里的进攻，在东南亚战区纵深不超过1500公里，朝鲜半岛不超过750公里。而且（中国举行）演习地区的地形与中亚、远东和外贝加尔地区十分相似。另外，包括北京军区、沈阳军区和兰州军区的部队参加了演习，这些部队都是针对俄罗斯、蒙古和中亚的。<br />
<br />
　　外界认为，中国向俄罗斯购买海空武器主要用于对抗台湾地区和美国，因此，中俄军队发生战争的可能性很小。但是，两国有可能在陆上发生冲突―――中国并没向俄购买任何陆军装备。近期，中俄两国军事技术合作正在中断，有种说法是由于俄军工企业无法对华提供所需武器和技术，另一种说法则是中国正考虑在可预见的未来可能对俄军队采取军事行动。<br />
<br />
　　最后要再次强调，可以推测，中国军政领导人正认真考虑在可预见的未来对俄罗斯及中亚国家实施进攻作战行动的可能，这一战争被认为是最有可能发生的。▲（作者是俄罗斯政治和军事分析研究所副所长亚历山大・赫拉姆奇欣，玉鹏译）<br />
