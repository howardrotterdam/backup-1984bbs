推销员来了――冒险家乐园――都在“系统”的支配之下――小角色杀死了国王<br />
<br />
<br />
在开始“征途”之前，吕洋玩的是另一款游戏《传奇》。她玩得并不好，仅仅当作一种周末的消遣。《传奇》计时收费，玩家购买在线时间用来升级和打造装备，所有的事情都费时费力。“比如你要打一个终极怪物，”吕洋说，“要跑很远的路，在迷宫里面转来转去，杀掉无数的小怪，可能耗一个晚上，你才跑到大怪的面前。”最让人崩溃的是，当历尽艰辛终于站到大怪面前，一不小心却被秒杀了！那么，从头开始跑路吧。<br />
<br />
<br />
吕洋可能从未得知，在她晕头转向地跑路时，一个叫“送礼只送脑白金”的玩家也在这款游戏中闯荡，他从来不耐烦那些烦琐的升级步骤，而是直接购买高级别账号；他成千上万地花钱，砸下最顶级的装备。以钱铺路，他在最短的时间内得到了最强大的威力。在这款典型的韩式“泡菜”游戏里，他试验出了自己独辟蹊径的玩法。这位玩家就是后来《征途》的老板史玉柱。<br />
<br />
<br />
2007年年中的一天，就在吕洋常去的这家网吧，一个推销员站到了正在“跑路”的吕洋面前。他衣着得体，面带微笑，用充满诱惑力的词汇介绍了一款名叫《征途》的新游戏。“绝对不用走迷宫，我们要的就是爽快。”吕洋记得他这样保证。<br />
<br />
<br />
吕洋就此和朋友们“踏上征途”。她的这些朋友们要么是医院的同事，要么是丈夫生意上的伙伴，空闲不多，但钱不少。他们很快发现，《征途》的确是一款爽快感十足的游戏，简直就是为他们这样的人设计的。<br />
<br />
<br />
要找系统角色接受任务，不用费力去找，点F键，下拉菜单里角色名字一一罗列，就像网络上的超链接，双击名字，就自动找到面前；要去某个地方，不用走迷宫，点开地图，找到地名，鼠标左键单击，一会儿就到了。<br />
<br />
<br />
很快吕洋就离开了“清源村”，热闹的“凤凰城”和辉煌壮丽的“王城”才是她的舞台。这位后起之秀被吸纳进了“楚国”最有威望的家族“桃花源”。单打独斗是不现实的，拜入名门正派才是出头之道，这里就如同武侠小说描写的江湖；而家族与家族联合可以组成帮派，帮派各自有各自的势力范围，在达成妥协之后又互相配合支撑起一个国家。<br />
<br />
<br />
游戏里的大部分系统角色，比如杂货店老板或是锻造铺师傅，都可以被帮会控制，此后他们会持续贡献“保护费”。系统鼓励帮会争夺控制权，谁杀死竞争对手，谁就能接手保护费。帮主们因此热衷于抢夺街区和店铺。这样在吕洋看来，她的置身之处，又像是1920年代的上海滩。<br />
<br />
<br />
这里的确像那个“冒险家乐园”。低级别的玩家甚至也可以挣钱！完成一些特定的任务，比如护镖，可以得到一定数量的“银两”；达到一定级别和保证一定在线时间后，系统甚至会给玩家“发工资”；还有各式各样的“奖励”、“返还”。<br />
<br />
<br />
“其实都是些小钱，”吕洋说，“相比花掉的钱简直没法比。”但由此带来的成就感和满足感是无法比拟的，你能想象在砍翻敌人的同时还能挣“银子”么？“想得到更高级的奖励么？”吕洋说，“那就花更多的钱呗。”<br />
<br />
<br />
时常有“外国人”冒险进入楚国，他们可能是魏国人，也可能是宋国人，或者是其他9个国家中任何一国人，但不管是哪国，一旦踏入楚国地界，他们的名字就显示为红色，这个颜色意味着：敌人――应当杀死的人。每杀死一个这样的人，系统就会在“护国英雄排行榜”里为你加上光荣的一分。<br />
<br />
<br />
他们并非是闲逛者，而是根据系统分配的指令而来，他们必须进入敌国完成诸如“刺探情报”或者“抢夺太庙资源”等等任务。而同样根据系统赋予的职责，家族和帮派此时的任务就是杀死他们。<br />
<br />
<br />
那时吕洋还是一个小角色，她正在砍杀一头犀牛，屏幕上出现一条指令，帮主撒下了帮主令，要求帮众前往杀敌。点击“是”之后，吕洋被传送到王城边境附近，众人正把敌人围在当中奋力砍杀。<br />
<br />
<br />
帮主“狼烟”是当然的主力，他是一个“武士”，近战肉搏是他的特长，他的“无双剑”能造成惊人的杀伤。在炫目的刀光闪耀间隙，吕洋突然发现敌人的坐骑是一只麒麟，这意味着他是一个国王，只有国王才拥有这种神兽！<br />
<br />
<br />
吕洋是一个“法师”，她专修冰系法术，可以从远处以冰雪为武器。这是她参加的第一次激战，她站到一个安全的地方，将霜冻从空中撒向敌人。麒麟居然哀号倒下，国王死了！吕洋幸运地完成了最后一击。<br />
<br />
<br />
屏幕上黄色的粗体字夸张地跳了出来：“天哪！吴国的国王居然被楚国的无名小辈女王杀死了！”吕洋在电脑前禁不住手舞足蹈，她杀死了国王，一个小角色杀死了国王！她双脚猛地一蹬，电脑黑屏了――她兴奋得把电源插头踢掉了。<br />
<br />
<br />
往后几天，吕洋都有些“神神叨叨”的。游戏里“全世界”都知道她杀死了国王，朋友们有些羡慕地向她祝贺。即使在医院上班，吕洋也在办公室的电脑上下载了游戏，她就是喜欢挂在游戏里，反复地看那条早就过时的消息：“天哪！吴国的国王居然被楚国的无名小辈女王杀死了！”<br />
<br />
<br />
有时候严厉的院长从办公室经过，威严地审视医生们的工作，吕洋只能把游戏界面最小化。她会悄悄地跟同事炫耀：“牛啥嘛，他要是耍游戏，我一招就把他砍翻！”院长其实并不坏，但吕洋觉得他“缺乏对女性应该有的尊重”。“在游戏里，女人也可以统治男人。”她宣称。<br />
