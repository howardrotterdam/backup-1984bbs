她最后一次上线是在不久前。那时征途网络公司已经宣布部分地改变了“绑定”规则，公司成功地在纽约上市，第三财季实现营业收入4.052亿元，净利润2.902亿元，相比去年同期，增长率达到了惊人的164％和152％。公司账面现金达68亿元人民币。<br />
<br />
<br />
又是一个财富神话。根据媒体的测算，史玉柱将以500亿身家荣登中国富豪排行榜的前列。<br />
<br />
<br />
系统开始慷慨地在游戏里“发行股票”，宣称随着股价上涨，玩家可以按同样的价钱兑换成“银两”。但吕洋对这些充满诱惑力的新玩意再也不关心了，她只是被一个朋友强拉着上线的，朋友要在游戏里“举行婚礼”。<br />
<br />
<br />
吕洋默不作声，藏身在前来庆贺的角色中，但是有一个人认出了她。这是一个武士，他手中的刀闪耀着光芒，显示着他强大的威力。“我认识你，”他说，“以前刚玩的时候你带过我们，现在我们也很厉害啦。”他们正在筹备新的国战，他告诉她。<br />
<br />
<br />
他是谁？是请她护过镖，还是曾经被她从敌国营救？吕洋完全不记得了，她只觉得这些越来越多的威风凛凛的后起之秀，跟以前的她是那么相像，一样地野心勃勃，一样以为发现了一个美丽新世界。<br />
<br />
<br />
她一句话也没回答，然后她下线了，并决定永远不再回来。<br />
<br />
<br />
