[原创]CCTV造假几时休？<br />
文章提交者：三风 加帖在 猫眼看人 【凯迪网络】 <a href="http://www.kdnet.net" target="_blank">http://www.kdnet.net</a><br />
<br />
CCTV造假几时休？<br />
<br />
前天吃饭时候偶尔和家人一起看了一会电视，非常郁闷的是家人看的cctv&nbsp;&nbsp;1，期间恰巧播放了一段所谓新闻，是关于魔兽世界的，讲的内容一如既往的是贬低魔兽世界和暴雪，说暴雪换代理如何如何不好，游戏如何如何血型，玩家如何如何花费巨资。。。<br />
<br />
而恰巧，我就是一个玩过魔兽世界的玩家，看了CCTV的新闻，我再次被CCTV震惊了。<br />
<br />
期间CCTV对暴雪和魔兽世界的贬低与讽刺我就不多说什么了，因为这是角度和价值观的问题，是见仁见智的问题，但CCTV的造假数据让我莫名惊诧，例如CCTV煽情的说，有的玩家玩魔兽世界买点卡已经花费了数万元，还有的玩家买高级装备已经花费了几十万云云。。。<br />
<br />
可是，很抱歉的说，这些数据都是CCTV造假的，为什么呢？LOOK：<br />
<br />
魔兽世界在中国正式收费运营始于2005年六月，到今天将近四年时间，魔兽世界一张点卡的柜台普通售价是28元（在网上购买更便宜），魔兽世界是没有外挂的，不能挂机的，那么一个人如果一天24小时不休息的玩，玩四年一共需要花多少钱买点卡呢？<br />
四年时间总共多少小时？<br />
24小时*30天*12个月*4年＝34560小时<br />
一张点卡66小时，零售价28元<br />
34560/66*28＝14661元<br />
<br />
<br />
也就是说，一个人，24小时不休息不停的玩，一共玩四年，不休息，不吃饭，不出门要花费14661元。。。<br />
<br />
那么CCTV说买点卡要几万元。。。买来干嘛？糊墙么？<br />
<br />
CCTV还说有的玩家买高级装备花费几十万。。。抱歉，那是传奇。。很遗憾的告诉CCTV，魔兽世界的高级装备都是绑定的，不能买卖（当然，也许CCTV清楚的很，但它就是骗那些不明真相的群众吧）<br />
<br />
你要买魔兽世界的高级装备怎么办呢？其实也有办法，那就是连绑定装备的号一起买了，现在各大魔兽网站和论坛都有练号工作室卖号，价钱不等，就我所见过的所有顶级级别，顶级装备的号多少钱呢？<br />
<br />
七十级顶级（魔兽世界有封顶级别70级，一般两个月左右就练到顶了）太阳井或顶级竞技场装备的号大概价钱在800元到1600元不等，价差与练号工作室和号的职业有关，含陆地顶级坐骑，含空中顶级坐骑，含目前大陆服务器顶级装备。。。<br />
<br />
也就是说，你一天都不用练，就卖那些绑定了顶级装备的号的话，最多花费1600元。。。<br />
<br />
那么CCTV说魔兽世界有人买装备花费几十万，能买多少个顶级装备绑定什么都有的人物呢？<br />
<br />
几十万，无三不成几，就算三十万，就算买最贵的工作室的顶级级别顶级装备的七十级号：<br />
<br />
300000/1600＝187个游戏人物<br />
<br />
一个玩家，买187个顶级装备顶级级别什么坐骑都有的号干嘛呢？&nbsp; &nbsp; 我看也就只能用来让CCTV造假玩了吧？<br />
<br />
<br />
本人做为一个魔兽世界的普通玩家，郑重的跟CCTV说一句：少造点假吧，你毕竟是国家级电视台。。。<br />
<br />
<br />
你造假，也要靠谱才行啊，我才能信呀，是不是呢？</div>

							
							
							
							
