所有人都必须是“人民币玩家”――和平受到鄙视，战争受到推崇――被杀死者得到的只有耻辱<br />
<br />
<br />
坐在常去的一家网吧的VIP包房里，吕洋显得兴奋而又忧心忡忡，她面前的19英寸液晶宽屏上，黄色的粗体字反复闪动：“国战将在晚上8点15分开始”。她还有两个小时，对她手下的战士们做最后的动员和部署。<br />
<br />
<br />
将要攻打楚国的是游戏中的头号强国魏国。魏国拥有上万名玩家，更为关键的是，魏国的支柱，“王者家族”成员们，个个都是令人敬畏的英雄，他们的等级遥遥领先于一般人，装备着成套的最为昂贵和罕有的神圣铠甲和武器。在“世界英雄排行榜”上，他们长期牢牢地占据着前五十名中最靠前的位置。<br />
<br />
<br />
他们拥有恐怖的杀伤力，人人以一当百。即使吕洋这个“女王”和他们对垒，如果战术采取不当，也存在被“秒杀”的危险。所谓秒杀，就是在几秒钟之内，在意图还击之前，就被一招致命。吕洋的丈夫并不玩游戏，但他也会对此开开玩笑：“就像武侠小说里写的，如果刀够快，你可以听见自己的血从脖子里喷出来的声音。”“女王”吕洋对他们最为忌惮，同时又略带鄙夷。“他们就是人民币玩家嘛。”她说。<br />
<br />
<br />
实际上，只要进了这个虚拟的小世界，所有人都必须是“人民币玩家”，区别只在于你愿意花多少而已。<br />
<br />
<br />
和以往的所有网络游戏不同，《征途》并不计时收费。简单地填写一份网上表格，就可以注册账号。只需要起个名字，选择性别、国家，一个虚拟身份就此诞生，光荣而艰难的征服之路摆在了你的面前。根据游戏的主线故事背景，你会惊奇地发现其实你是皇族之后，由于战乱被弃于荒野，现在你长大了，知道了自己高贵的血统，接下来该做的就是练一身好武艺，奋起于草莽之间，闻达于庙堂之上。建立自己的国家甚至取得统治世界的皇位以恢复祖先的荣光，则是你的终极目标。<br />
<br />
<br />
故事很合中国人的口味，惟一令人感到困扰的是：热血沸腾之后，你发现现实和理想的差距原来是那么大。一个新诞生的身份是1级；而王国里最英勇的英雄们能练到“转生170级”：练满普通人的168级之后，他们又获得新的不朽之身，并再次练到了170级。简单地说，那就是人和神的差距。英雄们手持 “完美的神圣武器”，浑身旋绕着代表高贵的紫色光晕；而你却两手空空，甚至全身上下只有一条遮羞的短裤。<br />
<br />
<br />
现在，你可以购买点卡，持续地把人民币注入游戏中的账户，以便快速升级，购买各种名贵材料打造装备；你也可以不花钱，如果什么都不做，只是呆在游戏里的话，系统不会向你收取一分钱。但很快你会发现，你连荒地里的一只蚊子都打不过，你的活动空间甚至仅限于出生地，一个叫“清源村”的小村子，更为广阔的世界则是为英雄们准备的。当然，最让你沮丧的还在于，贵为皇族的你，却永远生活在被其他玩家“秒杀”的巨大阴影之中。<br />
<br />
<br />
显然，吕洋走了一条真金白银铺就的英雄之路。她如今是“转生145级”，历经艰险戴上了王冠，她的坐骑是一条浑身闪着火光的龙。但英雄亦有英雄的烦恼，她的王国正面临严峻的挑战。<br />
<br />
<br />
吕洋自认为是一个“和平主义者”，她把女性的温婉带到了对国家的治理之中。“女王”很少主动攻击别国，更喜欢带着臣民们消灭怪物，或者经营骆驼商队。但这并不能避免受到攻击。<br />
<br />
<br />
“国战都是系统安排的，”吕洋解释说，“国王只需要提交申请，系统就会自动安排好战争爆发的时间。”<br />
<br />
<br />
系统喜欢“国战”。这个虚拟世界里，“和平”受到鄙视，战争受到推崇。战胜国可以自动获得战败国国库里的金钱和物资，国家排名由“国战”胜利次数决定，强国的臣民名字下闪耀着一颗颗星星，那代表着每一次辉煌的战胜。当然这并不是最重要的，对于战争中的人来说，冲入别国的土地，肆意砍杀每一个胆敢与之抗衡的人，无疑是释放内心深处原始冲动的绝佳办法。<br />
<br />
<br />
系统准确地捕捉着人性的弱点，召唤着玩家们在违背普世价值的虚拟世界中放纵自己的邪恶。它赋予战争中的人肆意杀戮的权力，给予杀人者加倍的经验值奖励；系统也会标明你的斩首纪录，那一串串数字就像印第安战士割下的头皮代表着无上的荣光，而被杀死者得到的只有耻辱。<br />
<br />
<br />
吕洋打电话，上QQ，或者在游戏中直接交谈，她自知实力对比悬殊，因此通知手下的战士们集中力量，守住王城的一角。她的声音因紧张而略带颤抖，尽管对现实中的她来说无论胜败都毫发无伤，但在游戏里，她知道那意味着生存还是毁灭。<br />
<br />
