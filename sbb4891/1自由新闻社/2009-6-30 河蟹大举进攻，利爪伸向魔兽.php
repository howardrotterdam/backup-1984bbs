国王万岁<br />
<br />
<br />
核裂变一样蔓延的仇恨――好的装备都意味着金钱――总有无数的人在疯狂比拼――“国王万岁！”<br />
<br />
<br />
但“女王”其实并不像想象中一样“牛”，几天之后，就在王城外，一个敌人逼近，他骑着鬃毛飞散的烈马，威严得像天上的神，他只问了一句话：“你就是那个女王啊？”一个巨大的火球从天上砸下，落单的“女王”还没来得及答话，被“秒杀”了。<br />
<br />
<br />
吕洋目瞪口呆，短暂的惊愕之后她脑子里只有一个想法，她要升级，要得到最好的装备，她要当真正的王，她要报仇！<br />
<br />
<br />
仇恨是这个世界中最强大的动力。“秒杀”吕洋的玩家，立刻被系统自动排入她的仇人名单，时时提醒“女王”报仇雪恨。“仇人”是这里最常见的社会关系，仇恨关系同样存在于家族、帮会和国家之间。如同核裂变一样蔓延的冤冤相报，在这里是受到鼓励和颂扬的永恒主题。<br />
<br />
<br />
她找到“代练使者”，一个代表系统与玩家做交易的虚拟角色，他就站在王城大道边上，答话谦卑而热情，乐意为有钱人服务。吕洋向“使者”支付人民币购买经验值，“使者”则让她迅速升级，从人变成了神。吕洋觉得这么做很值得，在这上面，她“只不过花了千把块钱”。<br />
<br />
<br />
但是，杀伤力和防护力是由装备决定的，级别只是佩戴相应等级装备的必须条件。就像金庸的小说，内力的修炼是必须的，但一把玄铁剑却可以让杨过无敌于天下。游戏中按照等级不同划分出了20等装备，以法师职业为例，武器由最初的柳木杖到最为罕有的至尊杖。此外还有铠甲、头盔、腰带、护腕、项链和戒指等等。<br />
<br />
<br />
这些好的装备都意味着金钱。和别的网络游戏不同，在这里，打怪和接任务不会掉装备。“我都要最好的，”吕洋说，“你只能去系统商店买材料，再找系统铸造商打造；或者，你只能去赌。”<br />
<br />
<br />
“赌”，就是“开宝箱”。玩家向系统购买钥匙和箱子，很便宜，一块钱一套。用钥匙点击开启，屏幕上一个光芒闪耀的箱子缓缓打开，各种材料或装备如同赌场大转轮一样排列在箱子里，光圈飞速转动，停下的地方就代表赌到的东西。箱子里经常会有玩家们最希望得到的高级装备，但转动的光圈却往往和它们擦肩而过。<br />
<br />
<br />
吕洋如今回忆，最疯狂的时候她就像一个赌场里的赌徒，会在屏幕前大声喊叫自己想要的东西，比如“乌木，乌木”，那是一种高级材料，而最后她往往得到的只是一点少得可怜的经验值。“乌木”，或者那威力强大的“幽冥戒指”，仍然摆在宝箱里，闪着诱惑的光。<br />
<br />
<br />
这个世界中还存在着一个“开宝箱”排行榜，每天开宝箱最多的人，可以得到一颗罕有的令人倍增威力的“补天神石”。<br />
<br />
<br />
这个巧妙的设置具有无穷的诱惑力，总有无数人在疯狂比拼开箱数量。吕洋曾一晚上开过上千个箱子，但始终功亏一篑，总有人比她更为执著，她从没拿到过那颗神奇的石头。<br />
