各式各样的排行榜随时闪现，世界英雄排行榜――根据级别和装备带来的威力排序；护国英雄排行榜――根据杀死敌国人的数量排序；国家实力排行榜 ――根据国家物资和国战胜负记录排序……就像电视里随时出现的脑白金广告，实时变动的排名对野心勃勃的英雄们高频率轰炸，提醒他们在这个严酷的世界里一刻也不能放松。<br />
<br />
<br />
玩家在不懈地开着箱子，他们有时好运地得到一点好东西，绝大部分时间几无所获，但越是没有收获，他们越是迫不及待地打开下一个箱子。他们每点击一下，就代表一块钱，又一块钱……就像一个永不停歇的沙漏，金钱涓滴成河一般随着每一次点击汇成巨流，流向看不见的系统。<br />
<br />
<br />
吕洋自认为运气还比较好，“平均开1000个箱子，好运的话能有十几次拿到好东西。”<br />
