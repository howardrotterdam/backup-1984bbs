<br />
“泡菜”开的花中国结的果<br />
<br />
<br />
韩式网游的“精髓”<br />
<br />
<br />
“中国玩家在欧服美服都被视为不受欢迎的异类。”一位曾在《魔兽世界》任职的游戏经理说。中国人总有办法以欧美玩家望尘莫及的速度升到极高的级别，在进行团队项目时也不喜欢遵守默认的利益分配规则。对于显得有些“迂腐”的欧美玩家来说，中国人就像是恐怖的异教徒。“欧美游戏并不鼓励力量上的无限优势，比较讲究平衡和互相牵制，”曾经的魔兽经理说，“可能是因为传统文化和现实环境的影响，说实话中国玩家还是更适合丛林式游戏。”<br />
<br />
<br />
一名网络游戏经理回忆，他曾经接待过一位跑到游戏公司的有钱但缺乏耐心的玩家。这位玩家专程前来咨询：是否可以直接付钱，购买顶级装备。公司上下当时哭笑不得。如今这名经理感叹，他们没有看到在这样的玩家身上，蕴含着巨大的商机，而《征途》看出来了，获得了成功。<br />
<br />
<br />
恃强凌弱和功利主义的“社会准则”来自于韩式网游。在被称为“泡菜”的典型韩国网络游戏中，玩家最常做的功课是练级，而练级的目的则是获得强大的威力和权力。没人能够否认这些游戏中的虚拟社会由对抗、暴力和欲望主宰，玩家们因此急功近利、恩怨分明、派系林立、残酷冷漠。这既是游戏的乐趣所在，亦是对人性弱点的敏锐捕捉。<br />
<br />
<br />
从《传奇》开始，韩式公会模式深入人心。这种模式极具东方式的家族色彩，对内严格管理，对外一致作战。行会会长可以自己制定行会会规，可以发出通缉令，与其他行会结盟或宣战。这种设置便于玩家们结成团体满足自己的战争欲望，同时也确立了集权式的“社会结构”。<br />
<br />
<br />
这一社会结构不只存在于虚拟之中。每一个玩家背后都是一个活生生的人。相当多的行会成员来自于一个城市甚至同一间网吧。行会内部的利益关系日益复杂，为了招募新手，行会可能会送装备、送钱。行会设置很多职位，成员之间等级森严，如同帮会。战争开始时各成员奋力杀戮，既获得暴力快感，又可以得到战利品，而战利品又意味着金钱。战利品的分配亦遵从等级制度。<br />
<br />
<br />
当韩式网游的“精髓”发挥到了极至，权力、荣誉和快感都来源于暴力，而暴力的最佳来源就是金钱。游戏设置亦乐于创造仇恨与贪欲，把玩家分为大大小小的家族、帮派和国家，设立各种个人或组织争抢的目标，甚至直接挑起争斗。事实上，这并非《征途》一款游戏之功或之罪，这种价值指向正是韩式网络游戏的传统精髓所在。<br />
<br />
<br />
据说陈天桥在了解了《征途》的模式之后，连夜召开高管会议。会议的结果是《传奇》全面推行《征途》式的“免费”模式。《征途》取得了一个又一个令业界瞠目结舌的成果，原先对史玉柱进军网游持嘲笑态度的游戏运营商们，开始纷纷潜心学习他的游戏设置。而征途网络公司近日已经将新开发的《巨人》投入公测，根据一句前后矛盾的宣传语，它号称“最便宜的免费网游”。“《征途》式”网游，“最中国”的网游，将越来越多地出现在玩家面前。<br />
<br />
<br />
这款最中国的游戏拥有最中国的玩家。除了《魔兽世界》等少数例子之外，不讲究权力与等级制度的欧美网游在中国玩家中并不风行。事实上，虚拟世界并不可以与现实世界简单对应，电子游戏中的“合法伤害权”亦是必然乐趣之一。问题在于玩家们在追求这种虚拟乐趣时是否会违背自己在现实生活中的伦理准则。<br />
