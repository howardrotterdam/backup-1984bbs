　　<br />
　　对此事的关注，是因为死者的叔叔恰好是我的高中同学，他和死者父母及其他亲属一块到上海处理这件事。他们处理的具体过程（主要是谈赔偿问题）我并没有参与，但今天早上的一个电话却让我震惊！<br />
　　<br />
　　电话是死者肖德坤的叔叔打来的，说他们全部被抓进了上海闵行区梅陇镇派出所，我听后相当震惊。我一边向那边赶，一边通过电话了解一下情况。<br />
