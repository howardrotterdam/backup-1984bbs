　　<br />
　　我11：30左右赶到梅陇镇派出所，见到了被看管的他们，他们早饭还没有吃，死者母亲因病发烧，多次反映，无人给找药，拨打120一报地点却被告知要打110，在派出所还要打110吗？真是荒唐！更荒唐的是，那几个戴墨镜的彪形大汉竟然不需要接受调查和看管，而是逍遥的坐在外边大厅了（同学一个个指给我看了）！更可气的是，派出所的民警一个个三缄其口，不愿意和我沟通，不愿意回答我的问题，一句话就是“上边会来人处理的”，值班的让我找所长，让我打值班领导热线，所长不见面，打值班领导热线竟然是停止服务的提示音（后来再拨打是关机、暂时无法接通），真是匪夷所思！<br />
