<br />
The dead of Dunhuang lie in a waste ground on the fringe of the desert, at the foot of great dunes where tourists ride on camels. Tombs, cairns and unmarked heaps of earth dot the boundless sands. <br />
沙漠边缘，沙丘脚下，敦煌的死魂安息在废墟中；游客骑着骆驼，踏着他们的尸骨走过。广漠无垠的沙漠里，坟墓、石冢，还有不知名的土丘星星点点。<br />
<br />
By local tradition, the clothes of the deceased are thrown away at their funerals. Dresses, suits and children’s garments lie half-buried by dust around the graves. <br />
