<br />
Revolt stirs among China’s nuclear ghosts<br />
核武冤魂<br />
<br />
Up to 190,000 may have died as a result of China’s weapons tests: now ailing survivors want compensation<br />
中国核武器测试夺去了将近190,000人的生命。现在，体弱多病的幸存者要求补偿<br />
<br />
The nuclear test grounds in the wastes of the Gobi desert have fallen silent but veterans of those lonely places are speaking out for the first time about the terrible price exacted by China’s zealous pursuit of the atomic bomb. <br />
戈壁滩的废墟中，核测试场地一片寂静；在这些偏僻角落，老兵们第一次道出，中国对原子弹的热烈追求，让他们付出了多么惨重的代价。<br />
<br />
They talk of picking up radioactive debris with their bare hands, of sluicing down bombers that had flown through mushroom clouds, of soldiers dying before their time of strange and rare diseases, and children born with mysterious cancers. <br />
他们道出赤手拾捡放射性残骸的经历，道出冲洗穿越过蘑菇云子弹的体验，道出自己的战友尚不足年就患怪病而死，道出自己的孩子刚出娘胎就得上了前所未有的绝症。<br />
<br />
These were the men and women of Unit 8023, a special detachment charged with conducting atomic tests at Lop Nur in Xinjiang province, a place of utter desolation and C until now C complete secrecy. <br />
他们来自8023部队。他们在缄口如瓶的秘密中，在渺无人烟的新疆罗布泊驻扎，特别指派执行原子弹试验。<br />
<br />
“I was a member of Unit 8023 for 23 years,” said one old soldier in an interview. “My job was to go into the blast zone to retrieve test objects and monitoring equipment after the explosion.<br />
一位接受采访的老军人说：“我在8023部队当兵23年。当时我们干的就是进入爆炸区寻找试验品，还有在爆炸后监控设备。”<br />
<br />
“When my daughter was born she was diagnosed with a huge tumour on her spinal cord. The doctors blame nuclear fallout. She’s had two major operations and has lived a life of indescribable hardship. And all we get from the government is 130 yuan [&pound;13] a month.” <br />
“我女儿出生的时候就被诊断出脊柱上有一个很大的肿瘤。医生说这是辐射落尘造成的。她动了两次手术，生活的艰难你真的不能想象。可是政府给我们的全部补助就是每个月130块钱[13英镑]。”<br />
<br />
Hardship and risk counted for little when China was determined to join the nuclear club at any cost. <br />
可是，在中国不惜一切代价进入核国家的决心面前，什么艰难，什么危险，都显得无足轻重。<br />
<br />
Soldiers galloped on horseback towards mushroom clouds, with only gas masks for protection. <br />
士兵跨上马背，奔向蘑菇云，他们的保护措施只有单薄的防毒面具。<br />
<br />
Scientists jumped for joy, waving their little red books of Maoist thought, while atomic debris boiled in the sky. <br />
原子弹碎片沸满天空，科学家们欢欣雀跃，挥动手中的***思想红宝书。<br />
<br />
Engineers even replicated a full-scale Beijing subway station beneath the sands of the Gobi to test who might survive a Sino-Soviet armageddon. <br />
戈壁的黄沙下，工程师们甚至全比仿建了北京地铁站，用以测试中苏一旦大战，谁人能够幸存。<br />
<br />
New research suggests the Chinese nuclear tests from 1964 to 1996 claimed more lives than those of any other nation. Professor Jun Takada, a Japanese physicist, has calculated that up to 1.48m people were exposed to fallout and 190,000 of them may have died from diseases linked to radiation. <br />
新调查显示1964年到1996年，中国核试验所造成的生命损失世界各国无有能及。日本物理学家高田润教授估算，这段时间有148万人民受到核放射的影响，其中190,000人可能死于放射引发的疾病。<br />
<br />
“Nuclear sands” - a mixture of dust and fission products - were blown by prevailing winds from Lop Nur towards towns and villages along the ancient Silk Road from China to the West. <br />
尘土和裂变产物交杂，形成“核沙”――常年不断的风暴刮过，把它们吹出罗布泊，吹向沟通中西的丝绸之路沿途的城镇和村庄。<br />
<br />
The victims included Chinese, Uighur Muslims and Tibetans, who lived in these remote regions. Takada found deformed children as far away as Kazakhstan. No independent scientific study has ever been published inside China. <br />
受害者是居住在这些偏僻的地区的汉人、维吾尔穆斯林和藏民，甚至还波及哈萨克斯坦――高田在那里也找到了畸形儿童。可是中国境内，却从未出版过一篇相关的科学研究。<br />
<br />
It is the voices of the Chinese veterans, however, that will reso-nate loudest in a nation proud of its nuclear status but ill informed about the costs. One group has boldly published letters to the state council and the central military commission - the two highest government and military bodies - demanding compensation. <br />
在这个以核武器地位为豪，却对其代价一无所知的国度，老兵们的声音响彻南北。其中一个老军人组织不畏艰险印发了一批信件，投往政府最高级别的两家军事机构――国务院和中央军委。<br />
<br />
“Most of us are between 50 and 70 and in bad health,” they said. “We did the most hazardous job of all, retrieving debris from the missile tests. <br />
他们说：“我们这些人，小的也有五十多岁，大的都七十多了，身体都差的很。当时我们干的时最险恶的工作――在导弹试射中寻找残骸。”<br />
<br />
“We were only 10 kilometres [six miles] from the blast. We entered the zone many times with no protective suits, only goggles and gas masks. Afterwards, we just washed ourselves down with plain water.” <br />
“爆炸离我们只有10公里[6英里]。很多次我们进入试验区域的时候，除了护目镜和防毒面具，什么保护装备都没有。完成任务之后，用清水洗洗就完了。”<br />
<br />
A woman veteran of Unit 8023 described in an interview how her hair had fallen out. She had lost weight, suffered chronic insomnia and had episodes of confusion. <br />
8023部队的一位女兵在一次采访中，说自己头发脱落，日益瘦削，长期失眠，还会时常神志不清。<br />
<br />
“Between 1993 and 1996 the government speeded up the test programme, so I assisted at 10 underground explosions,” she said. “We had to go into the test zone to check highly radioactive instruments. Now I’m too sick to work - will the government help me?” <br />
她说：“1993年到1996年之间，政府加快了测试项目的步伐，然后我们参与了10次地下爆炸。我们需要进入试验区检查高放射性的设备。现在我身子很虚――政府能不能帮帮我啊？”<br />
<br />
The price was paid by more than one generation. “My father was in Unit 8023 from 1967 to 1979, when his job was to wash down aircraft that had flown through the mushroom clouds,” said a 37-year-old man. <br />
惨痛的代价延及后代。一名37岁的男子说：“我父亲1967年到1979年间在8023部队从军，他当时的工作是清洗穿过蘑菇云的飞行器。”<br />
<br />
“I’ve been disabled by chronic immune system diseases all my life and my brother’s daughter was born with a heart defect,” he said. “Our family has spent thousands of yuan on operations over the decades. Two and three generations of our family have such illnesses - was it the nuclear tests? Does our government plan any compensation?” <br />
“我这儿一辈子都因为慢性免疫系统疾病落下了残疾，我侄女有先天性心脏缺陷。几十年来，我们一家动手术就花了好几千了，家里两三代都是这种病――这是不是核测试落下的？那我们政府有没有计划给我们什么补偿？”<br />
<br />
In fact, the government has already responded to pressure from veterans’ groups. Last year Li Xueju, the minister of civil affairs, let slip that the state had started to pay “subsidies” to nuclear test personnel but gave no details of the amounts. <br />
事实上，政府已经迫于老兵组织的压力，予以回复。去年，民政部部长李学举透露，政府已经开始向核测试的相关人员支付“补助金”，但是对于数目细节，他却缄口不言。<br />
<br />
Such is the legacy of the decision by Chairman Mao Tse-tung, in 1955, to build the bomb in order to make China a great power. <br />
1955年，***主席为了将中国建设成强国，开启了原子弹研究――这就是后果。<br />
<br />
Mao was driven by fear of the US and rivalry with the Soviet Union. He coveted the might that would be bestowed by nuclear weapons on a poor agricultural nation. Celebrations greeted the first test explosion on October 16, 1964. <br />
美国的力量，苏联的竞争，都让***坐立不安。他暗示，核武器将会赋予当时还是农业穷国的中国无限威力。1964年10月16日第一颗原子弹爆炸成功，举国欢庆。<br />
<br />
The scientists staged a total of 46 tests around the Lop Nur site, 1,500 miles west of Beijing. Of these tests, 23 were in the atmosphere, 22 underground and one failed. They included thermonuclear blasts, neutron bombs and an atomic bomb covertly tested for Pakistan on May 26, 1990. <br />
首都1,500公里外的罗布泊，科学家先后测试46次，其中23次为大气爆炸，22次地下爆炸，还有一次失败。这些爆炸中包括热核，中子弹，和1990年5月26日秘密为巴基斯坦爆炸的原子弹。<br />
<br />
One device, dropped from an aircraft on November 17, 1976, was 320 times more powerful than the bomb that destroyed Hiroshima. <br />
某1976年11月7日，从飞行器上掉落的装置，威力就是广岛原子弹的320倍之多。<br />
<br />
The last explosion in the air was in 1980, but the last underground test was not until July 29, 1996. Later that year, China signed the Comprehensive Test Ban Treaty and, once again, only the sigh of the winds could be heard in the desolation of the Gobi desert. <br />
最后一次空中爆炸在1980年，但是直到1996年7月29日，地下测试才得以收尾。就在当年晚些时候，中国签署了《全面禁止核试爆条约》――戈壁滩的荒芜中又一次，只留风吟。<br />
<br />
The financial cost remains secret, but the price of the first bomb was roughly equal to more than a third of the entire state budget for 1957 C spending that went on while at least 30m Chinese peasants died of famine and the nuclear scientists themselves lived on hardship rations. <br />
其中投入财力未曾透露，但是第一枚原子弹的花费大约是1957年全年国家预算的1/3之多――就在那一年，至少3千万中国农民死于饥饿，而核专家们的生活也只得靠困难配售维持。<br />
<br />
Rare was the outsider who gained a glimpse of this huge project. One was Danny Stillman, director of technical intelligence at Los Alamos, New Mexico, home of America’s nuclear weapons. He made 10 visits to secret Chinese nuclear facilities during a period of detente and information exchange from 1990 to 2001. <br />
能够对此巨大工程瞥见一斑的外界人士少之又少。在美国核武器的发源地新墨西哥，洛斯拉莫斯实验室主任丹尼.斯蒂尔曼就曾有幸得见。1990年到2001年，在双边政策有所缓和、信息交流之时，他曾十度秘密参观中国核设施。<br />
<br />
“Some of the videos they showed me were of PLA [People’s Liberation Army] soldiers riding on horses - with gas masks over the noses and mouths of both the horses and the soldiers - as they were riding towards the mushroom cloud of an atmospheric surface detonation,” Stillman recalled. <br />
“当时他们给我看了一些录像，其中有的是解放军在大气表面爆炸后，骑马向着产生的蘑菇云的方向行动，人和马都罩着防毒面具保护口鼻。”斯蒂尔曼回忆道。<br />
<br />
“It was strange because the soldiers had swords raised above their heads as they headed for the radioactive fallout. I have always wondered how many of them survived.” <br />
“我看到士兵们向着放射性尘降走去的时候，会把刀剑高举过头顶，当时真的觉得很奇怪。我一直想知道，这些人里面能活下来的有多少。”<br />
<br />
Stillman was also allowed to see the lengths to which the Chinese scientists had gone to experiment with annihilation in the desert. <br />
斯蒂尔曼还得到允许，观看中国科学家在沙漠中进行毁灭性试验的全部过程。<br />
<br />
Like the Americans, the Chinese placed caged live animals, tanks, planes, vehicles and buildings around test sites. Such were the remains gathered by the men and women of Unit 8302. <br />
这点中国人同美国人大同小异，也在试点周围放置了笼养活动物，坦克，飞机，车辆和建筑。而8302部队的男女官兵们，就负责将这些残骸收集起来。<br />
<br />
“The surprise to me was that they also had a full-scale Beijing subway station with all supporting utilities constructed at an undefined depth directly underneath,” said Stillman. <br />
“我觉得很吃惊的是，他们居然在地下不知道多深的地方建造了一个同等比例的北京地铁站，辅助设施一应俱全。”斯蒂尔曼说。<br />
<br />
“There were 10,000 animals and a model of a Yangtze River bridge,” recalled Wu Qian, a scientist. <br />
“那儿有上万的动物，还有一长江大桥的模型。”科学家吴潜（音）回忆到。<br />
<br />
Li Yi, a woman doctor, added: “Animals placed two kilometres from the blast centre were burnt to cinders and those eight kilometres away died within a few days.” <br />
一位名为李艺（音）的女医生补充：“爆炸中心两公里以外的动物都燃为灰烬，八公里以外的没几天就死了。”<br />
<br />
China had borrowed Soviet blueprints and spied on the West, according to The Nuclear Express, a book by Stillman and Thomas Reed, the former US air force secretary. <br />
根据斯蒂尔曼和前美国空军部长托马斯.里德合著的《核快车》一书内容，中国从苏联借来核武蓝图，又剽窃了西方的技术。<br />
<br />
It explains how China then exploited its human capital to win technological parity with the US for just 4% of the effort - 45 successful test explosions against more than 1,000 American tests. <br />
这就解释了为什么中国获得同美国平等的核武地位，却仅仅使用美国4%的人力资源――美国人经历了上千次成功测验，而中国人只有45次。<br />
<br />
“The Chinese nuclear weapon scientists I met . . . were exceptionally brilliant,” Stillman said. <br />
“我所遇到的中国核专家……都太强了。”斯蒂尔曼说。<br />
<br />
Of China’s top 10 pioneers, two were educated at Edinburgh University - Cheng Kaijia, director of the weapons laboratory, and Peng Huan-wu, designer of the first thermonuclear bomb. Six went to college in the United States, one in France and one in Germany. <br />
中国顶尖的10位专家中，核武器研究所所长程开甲和第一枚热核炸弹设计者彭桓武，都曾在爱丁堡大学留学；6人曾在美国进修，1人在法国，另外还有一人在德国。<br />
<br />
For all this array of genius, no Chinese scientist has dared to publish a study of the human toll. <br />
虽然不乏才干，却没有中国科学家敢于公布死亡人数。<br />
<br />
That taboo has been broken by Takada, a physicist at the faculty of medicine at Sapporo University, who is an adviser on radiation hazards to the government of Japan. <br />
但这个禁忌，已经被札幌大学医学院物理学家，日本政府辐射危害顾问高田打破。<br />
<br />
He developed a computer simulation model, based on fieldwork at Soviet test sites in Kazakhstan, to calculate that 1.48m people were exposed to contamination during 32 years of Chinese tests. <br />
在哈萨克斯坦的苏联试点作业基础上，他开发一个计算机模拟模型，由此计算出中国32年的试验中，148万人都受到了核辐射污染。<br />
<br />
Takada used internationally recognised radiation dosage measurements to estimate that 190,000 have died of cancer or leukaemia. He believes 35,000 foetuses were deformed or miscarried, with cases found as far away as Makanchi, near the Kazakh border with China. <br />
高田使用国际认可的辐射剂量测量方法，测定出190,000人死于癌症或白血病，并且认为多达35,000的胎儿都因此畸形或流产，影响远达哈中交界处附近的马侃池。<br />
<br />
To put his findings in perspective, Takada said China’s three biggest tests alone generated 4m times more radioactivity than the Chernobyl reactor accident of 1986. He has called the clouds of fallout “an air tsunami”. <br />
为使自己的发现更加准确，高田说，仅仅中国三次最大的测试，产生的辐射就已经是1986年切尔诺贝利反应堆事故的400倍，他将测试中辐射微粒云称为“空气海啸”。<br />
<br />
Despite the pall of silence inside China, two remarkable proofs of the damage to health have come from official Communist party documents, dated 2007 and available on provincial websites. <br />
虽然中国国内仍然被寂静笼罩，但是中共中央文件中却透漏了核试验对人类健康造成危害的两项重要文件――日期标注2007年，在省级网站上仍可获得。<br />
<br />
One is a request to the health ministry from peasants’ and workers’ delegates in Xinjiang province for a special hospital to be built to cope with large numbers of patients who were “exposed to radiation or who wandered into the test zones by mistake”. <br />
其一就是新疆工农代表向卫生部发出的，建立一家特别医院来治疗大量“受到辐射影响或者无意走进试点区域的人们”的请求文件。<br />
<br />
The other records a call by a party delegate named Xingfu for compensation and a study of “the severe situation of radiation sickness” in the county of Xiaobei, outside the oasis town of Dunhuang. <br />
另外就是一些报告，分别记录了一位名为兴福（音）的党代表呼吁进行补贴，和一项绿洲城镇敦煌外部小北县“辐射病严峻形势”的研究。<br />
<br />
Both claims were rejected. Residents of Xiaobei report an alarming number of cancer deaths and children born with cleft palates, bone deformities and scoliosis, a curvature of the spine. <br />
但是两要求都遭到了拒绝。小北县居民说，当地癌症死亡率惊人，新生儿也经常会患有血小板破裂，骨骼畸形，脊柱侧凸。<br />
<br />
Specialists at hospitals in three cities along the Silk Road all reported a disproportionate number of cancer and leukaemia cases. <br />
丝绸之路上三个城市的医院中，专家都称癌症和白血病死亡率异常。<br />
<br />
“I have read the Japanese professor’s work on the internet and I think it is credible,” said one. No cancer statistics for the region are made public. <br />
“我从网上读到了日本那位教授作的研究，我觉得真的难以置信。”一位专家这样说。但是该地区的癌症数据却从未公布于众。<br />
