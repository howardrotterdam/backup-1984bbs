Some memories, though, remain indelible. One man in Dunhuang recalled climbing up a mountain-side to watch a great pillar of dust swirl in from the desert. <br />
有的记忆却难以忘怀。居住在敦煌的一位男子，就清晰的记得曾经爬上山腰，看到沙漠中一支巨大的烟雾云在空中呈漩涡翻滚。<br />
