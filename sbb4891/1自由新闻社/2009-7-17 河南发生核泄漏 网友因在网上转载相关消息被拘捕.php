&nbsp; &nbsp; 7月17日，河南开封杞县流传钴60将爆炸的谣言，许多群众逃离家乡，前往附近县市“避难”。当地政府紧急应对，采取各种方式辟谣。河南省委书记徐光春作出批示，副省长秦玉海赴杞县指挥。据当地媒体报道，绝大部分出走的群众已返乡，县城商铺也陆续开门，社会秩序趋于稳定。<br />
<br />
&nbsp; &nbsp; 昨天，开封警方宣布“抓获杞县钴60事件5名造谣者”，其中一名曾经转帖虚假消息的网友被拘留。而在百度杞县吧等网络论坛上，有网友质疑有关部门信息不公开，“为什么有谣言，就是因为你不说。你不说，其他人就说，其他人说，就会乱说。”<br />
<br />
&nbsp; &nbsp; 转载网帖被拘留<br />
<br />
&nbsp; &nbsp; 据当地媒体报道，开封市市长周以忠要求公安部门对造谣者立案侦查，迅速查清谣言源头及传播途径，对犯罪嫌疑人要绳之以法，并在媒体上曝光。昨天赶到现场的河南省副省长秦玉海也提出类似要求。<br />
<br />
&nbsp; &nbsp; 昨天，开封警方成功“抓获杞县钴60事件5名造谣者”。据开封网报道，个别别有用心的人日前在群众中和网上发布谣言，说“发生了爆炸”，出现了危险，造成部分不明真相群众恐慌，影响社会稳定。发现情况后，公安机关迅速行动，开展调查取证，并抓获事件造谣者5名。其中，开封县居民张某7月14日从腾讯论坛转到天涯论坛的一个帖子，内容为“……现在科学家已经没有办法了，许多外国的科学家都过来了，可还只是坐以待毙！都没有招了……”在张某承认后，“开封县公安局以涉嫌编造、故意传播虚假恐怖信息罪将张某刑事拘留”。“另外4名传播虚假信息者分别被公安机关根据《治安管理处罚法》给予治安处罚。”<br />
<br />
&nbsp; &nbsp; 记者注意到，5人中被惩罚最严厉的张某，“造谣”的行为就是转载了一个虚假的帖子。记者查询发现，最晚在7月10日该帖就已经出现在天涯论坛，并在不同论坛被多位网友转载。帖子的内容与17日引发恐慌的“钴60将爆炸”谣言看不出明显的关系。这个帖子在网上流传至少4天后张某进行了转载，然后成为了唯一被拘留的网民。<br />
<br />
&nbsp; &nbsp; 网友质疑信息不公开<br />
<br />
&nbsp; &nbsp; 开封媒体提出“谣言误民害民”，很多当地网友也都对谣言表达了强烈的愤慨。同时，也有很多网友提出，谣言的出现，与有关部门信息公开不及时、不透明有关。<br />
<br />
&nbsp; &nbsp; 6月7日发生故障，政府一直不公开情况，直到网上炒成一片了，当地政府才在7月12日第一次发布消息，网民对此意见很大。“封锁信息整整一个月，你解释说怕引起慌乱！难道你不知道隐瞒实情更会引起慌乱？”网友“晴天乖娃娃”17日晚9时44分在天涯跟帖提出质疑，他说自己刚刚从杞县逃到郑州，“谣言……为什么有谣言？就是因为你不说。你不说，其他人就说；其他人说，就会乱说。他们没亲眼看到，只能这样传。”<br />
<br />
&nbsp; &nbsp; 事件发生后，有一种很流行的说法是“谣言止于智者”，有些网帖也批评乃至嘲笑当地民众相信粗劣谣言，盲目跟风逃离。这让一些当地网友非常愤怒，流传很广的一个帖子《七月十七日，哭泣的杞县》这样写道：“这个社会没有愚味的群众，只有愚味的领导！老百姓是不懂，但是，谁也不能笑话老百姓！还没有发生恐慌性溃逃的时候，官员专家们都去哪里了啊？当杞县的老少爷们、妇女儿童冒着近40℃的高温仓惶往外逃的时候，当他们根本不知道发生了什么样的事情，却连家都不要了也要逃命的时候，有一个所谓的明白人来说一句话吗？”<br />
<br />
&nbsp; &nbsp; 据了解，群众从中午1点左右开始逃离，下午3点杞县县委、县政府开始开会研究部署措施时，正是群众出走的高峰。下午5时许，当地开始通过电视、网络、手机、宣传车等多种渠道公开信息、澄清真相、安定民心，很快起到作用，出走民众陆续返乡。有网友提出，这说明只要公开透明信息老百姓是会相信的，问题就这么容易解决，“如果早一点这么做不是更好吗？”<br />
<br />
&nbsp; &nbsp; 事发前当地拒绝通报情况<br />
<br />
&nbsp; &nbsp; 事实上，就在17日“发射源将爆炸”谣言兴起前两天，即7月15日，河南电视台都市频道就曾播出《杞县钴60泄漏事件调查》节目，在节目末尾部分质疑认为，杞县政府部门对此事的处置方法“无法理解”。而这也是本地媒体中为数不多的“批评声音”。<br />
<br />
&nbsp; &nbsp; 这段河南电视台网上至今存在的节目脚本显示：节目中记者就“当地报纸电视有没有解释”采访当地居民，受访者则称“谁解释？没人解释”。<br />
<br />
&nbsp; &nbsp; 随后，节目带有点评性地指出，“即使记者赶到杞县采访的时候，杞县相关领导对此事的态度仍然是三不政策：不通报情况，不接受采访，不允许报道。在网络越来越发达，透明度越来越高的今天，杞县的做法的确让人无法理解。” 本报记者 张东锋<br />
<br />
&nbsp; &nbsp; 开封环保局：<br />
<br />
&nbsp; &nbsp; 8月底解决故障<br />
<br />
&nbsp; &nbsp; 官员称卡源故障可以看作是“正常工作状态的延续”<br />
<br />
&nbsp; &nbsp; 6月7日发生卡源故障，7月17日环保部专家组来到现场利用机器人探查情况，那么故障究竟什么时间才能解决呢？昨天，开封市环保局副局长李春生接受开封网记者采访时表示，争取8月底前完成。<br />
<br />
&nbsp; &nbsp; 李春生说，发生卡源故障后，辐照室内的剂量较大，人不能直接进入辐照室内进行相关操作，这就给处理卡源故障带来了很多技术难题。尽管国内外以前也曾发生过类似卡源故障，但由于每座辐照装置均有其特殊性，原有的处置技术不能照搬，需要根据现场的具体情况制定科学的有针对性的处置方案。处置工作需坚持积极、稳妥、科学、安全的原则，由于这项工作专业性强、技术难度高，处置卡源方案的制定、论证以及加工针对该厂卡源故障的机器人，都需要一定的时间。为此，预计8月底前可在加快机器人改造进度的前提下完成处置工作。<br />
<br />
&nbsp; &nbsp; 李春生认为，这次卡源故障不是事故，而可以看作是“正常工作状态的延续”。他说，该厂的放射源无论是在正常工作状态还是在井下贮存，均不会对环境造成危害，这次卡源故障就是在其正常工作后未能降到正常的贮存状态，可看作是正常工作状态的延续。</div>

							
							
							
							
