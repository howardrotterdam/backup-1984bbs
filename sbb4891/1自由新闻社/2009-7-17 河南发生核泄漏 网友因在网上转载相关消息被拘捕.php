													<h2>环保部答记者问</h2>
						
						
																											<div id="postmessage_166361" class="t_msgfont">河南杞县放射源卡源事件未造成环境污染<br />
<a href="http://www.zhb.gov.cn/xcjy/zwhb/200907/t20090716_156241.htm" target="_blank">http://www.zhb.gov.cn/xcjy/zwhb/200907/t20090716_156241.htm</a><br />
河南省杞县利民辐照厂近日发生放射源卡源事件，未造成环境污染及人员辐射伤害。环境保护部有关负责人就该事件相关情况回答了记者的提问。<br />
<br />
　　<br />
<br />
　　问：河南省杞县利民辐照厂放射源卡源事件是怎样发生的？<br />
<br />
　　答：河南省杞县利民辐照厂是一家从事辐照加工的民营企业，于2006年9月取得我部颁发的辐射安全许可证。该企业辐照装置采用远程控制的钴－60放射源照射物品，使被照射物品达到灭菌、消毒等目的。该类装置的放射源处于1米厚的钢筋混凝土结构的辐照室内，进行辐照加工时，通过远程控制将放射源从水井中提出照射物品，使用后放射源即返回到水井中。2009年6月7日凌晨2时，该企业辐照装置在运行中发生货物意外倒塌，压住了放射源保护罩，并使其发生倾斜，导致钴－60放射源卡住，不能正常回到水井中的安全位置。<br />
<br />
　　6月14日15时，辐照室内原辐照加工的物品由于放射源的长时间照射，发生了升温自燃。在消防及环保部门采取灌注水等措施后，引燃物于当晚24时得到有效控制。经河南省辐射安全技术中心监测，附近环境未发现任何辐射污染现象。<br />
<br />
　　问 ：此次卡源属于辐射事故吗？<br />
<br />
　　答：发生卡源时，辐照装置正处于工作状态，没有任何人员处于辐照室内，事件未造成人员误照和辐射伤害。由于工业辐照用钴－60属于固体密封源，事件中放射源的不锈钢双层外壳没有遭到直接外力打击，包壳内的放射性物质没有泄漏，没有造成环境污染。按国家对辐照事故的分级管理规定，本次卡源不属于辐射事故，是辐射工作单位的运行事件。<br />
<br />
　　问：环境保护部对该事件持何态度，下一步如何处理？<br />
<br />
　　答：这是一起影响安全的运行事件。事发前，环境保护部在对该企业的监督检查中，发现了安全隐患，提出了限期整改要求。在整改期间发生卡源事件，说明该企业安全意识淡薄，整改措施没有及时落实。事件发生后，作为核与辐射安全监管部门，环境保护部高度重视，及时派出有关监督人员和技术专家赴现场监督检查，目前正在依法调查，如发现违法行为，将依法严肃查处。同时，环境保护部已要求河南省有关方面加强对该企业辐照室的人员出入控制和周围环境监测，切实保障公众和环境安全。<br />
<br />
　　问：该事件是否有进一步恶化的可能？<br />
<br />
　　答：杞县利民辐照厂已会同有关专家编制完成事件处理方案，我部正在组织专家进行论证。只要妥善采取经过科学论证的处理措施，将放射源收回到安全水井内，就不会造成对人员和环境的威胁。目前，发生卡源的辐照装置处于有效受控状态，事件不会进一步恶化。</div>

							
							
							
							
