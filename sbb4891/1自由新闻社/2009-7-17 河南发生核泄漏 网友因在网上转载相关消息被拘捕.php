																											<div id="postmessage_166150" class="t_msgfont">环保部：杞县放射源事件未发现环境污染 <br />
<a href="http://www.sina.com.cn" target="_blank">http://www.sina.com.cn</a>&nbsp;&nbsp;2009年07月17日 08:28&nbsp;&nbsp;南方都市报<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;<br />
本报讯 (驻京记者 杨章怀 实习生刘春瑞)昨日，环境保护部有关负责人对河南省杞县利民辐照厂放射源卡源事件作出回应，称经河南省辐射安全技术中心监测，附近环境未发现任何辐射污染现象。<br />
<br />
6月7日凌晨2时，河南省杞县利民辐照厂辐照装置在运行中发生货物意外倒塌，压住了放射源保护罩，并使其发生倾斜，导致钴-60放射源卡住，不能正常回到水井中的安全位置。<br />
<a href="http://finance.sina.com.cn/g/20090717/08286492831.shtml" target="_blank">http://finance.sina.com.cn/g/20090717/08286492831.shtml</a><br />
<br />
记者刘永喜 实习生毛慧敏报道&nbsp; &nbsp;昨日，市政府就杞县利民辐照厂卡源故障召开新闻发布会。目前，处置方案已编制完成，放射源至今完全处于安全状态。市政府秘书长马璞主持发布会。市环保局局长李凤刚通报处置情况。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;据李凤刚介绍，6月7日2时，该厂在完成辐照辣椒粉作业后进行降源时，发现放射源无法降入放射源井内，造成卡源故障。企业负责人立即将卡源情况上报了省环保厅、市环保局及环保部北方核与辐射安全监督站。该厂是一家民营企业，自1997年建成投运至今，从事辐照作业，主要用于对方便面调料包、辣椒粉、中药材、大蒜等辐照灭菌，其持有环境保护部颁发的辐射安全许可证。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;接到报告后，省环保厅、市政府高度重视，迅速指派有关专家在第一时间赶赴现场。经调查确认：放射源未能降入放射源井内的原因是因被辐照的货物倒塌，造成放射源护源架倾斜。根据该情况，省环保厅会同专家立即采取了5项措施：一是启动应急预案。市环保局、杞县人民政府、杞县环保局负责日常监管，省、市环保部门各派一名专业技术人员进驻现场加密监测，并实行日报告制度。二是在周边区域设立明显标志，严禁无关人员进入警戒区域；实行双人双岗24小时值班，严格执行安全保卫制度。三是责成业主尽快委托有资质的处置单位制订处置方案，聘请专家评审论证，将确定的处置方案上报环保部批准后实施处置。四是在处置方案未获批准前不得擅自行动。五是处置过程要充分尊重专家意见，科学处置，确保万无一失。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;6月14日15时，放射源室内辣椒粉因长时间受到辐照积热发生自燃。有关部门采取了注水等措施，引燃物于次日零时得到有效控制。经省职业病防治所对注入源室内的水进行采样分析监测显示，水体及周边环境未受到污染。6月15日，环保部核安全司放射源处及北方核与辐射安全监督站派出专家组赶到现场指导帮助处理工作。他们认为，河南省各级政府和环保部门对故障高度重视，且组织严密，措施得力，确保了放射源处于安全状态。同时要求今后一要继续注水，防止货物燃烧，保证放射源安全，确保现状稳定。二要尽快制订处置方案，报批后实施，彻底解决卡源问题。按照要求，6月20日，该厂与西南科技大学达成了协议。目前，“机器人降源处置方案”已编制完成，待专家评审论证并经环保部审批后即可开始处置。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;李凤刚说，此次卡源故障未发生人员伤害。7月11日，省辐射安全技术中心最新监测结果显示：周边环境辐射剂量为0.09～0.12微戈瑞/小时，低于省环境天然辐射剂量率平均值0.134微戈瑞/小时，放射源至今完全处于安全状态。根据原国家环保总局有关辐射事故分级的规定，该卡源情况不属于辐射事故。<br />
