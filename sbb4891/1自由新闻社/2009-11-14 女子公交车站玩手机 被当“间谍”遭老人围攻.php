来源：中国新闻网 2009年11月14日<br />
　　22岁女子谭佳在站台上玩手机，却被7名老人认作是在对老人实施高科技辐射监控。老人们将谭佳的面部抓伤后，还隔着公交车窗户对谭佳进行轮番谩骂。这起闹剧迫使一辆120路公交车停运2个多小时。<br />
<br />
　　女子：无聊在玩手机<br />
<br />
　　谭佳是广西人，4月份来到昆明，现在是新世界百货的一名营业员。昨日9时许，谭佳在国防路公交站台等73路车上班，闲着无聊，她玩起了自己的手机。突然，一位老太太跑过来质问她：“谁叫你对我进行非法监控的？你是不是卧底？”谭佳感到莫名其妙，还没等她反应过来，这名老太太就狠狠地踩了她一脚。于是，谭佳和这位老人发生争执并报了警。<br />
<br />
　　不一会，一辆120路公交车驶来，这名老太太就往车上走。“我被打了，当然不服气，必须等警察来处理，我就追上去拉住她。”老人的几位同伴见状，纷纷围了上来，“他们有的揪住我的头发，有的在后边使劲推，将我硬生生塞到了车上。”在拉扯过程中，谭佳的脸上留下一道深深的抓痕，左手手指也被铁片划伤。<br />
<br />
　　新村派出所的民警赶到后，这些老人拒绝下车。一名路过的女市民认为，老人们的行为影响了其他乘客的出行。没想到，其中两名老太太立刻下车对这位女市民进行追赶，女市民穿过了马路，车流将老人挡了回去。<br />
<br />
　　而在120路公交车上，几名老人仍未泄愤，他们隔着车窗玻璃，对着谭佳大骂。“他们用方言骂我，还骂所有衣服穿得好看的人，骂得很难听。”<br />
<br />
　　“从小到大，爸爸妈妈都没有打过我。”站在路边的谭佳身体微微发抖，她满脸委屈，眼睛湿了。为了证明自己非“卧底”，她还出示了自己的工作证。<br />
<br />
　　老人：被高科技监控了<br />
<br />
　　怀疑自己被“高科技辐射监控”的老人叶建华今年60岁，她说，他们一行7人于11日来到昆明。昨天上午，在站台上，她发现一个女子用手机对准她，“她用手机将我的头像录下来，做成图片和光碟，最后对我喷化学毒液谋杀我，毒液喷到哪个部位，哪个部位的血管就会阻塞，她威胁到了我的生命。我以前就被这么监控过，弄得我脑袋疼、心口疼。”叶建华说，对她实施跟踪监控的女子，肯定是“间谍”。<br />
<br />
　　一些旁观者说，老人的做法太偏激。一名路过的老太太安慰谭佳，让她不要和这些老人计较，“赶快去医院，要留什么证据还来得及。”新村派出所一名民警说：“他们都是老人，还真不好处理。等他们气消了，就好了。”警方将联系这些老人的家人，让老人的家人承担谭佳的医疗费和精神损失费。首席记者刘木木 (来源：生活新报)</div>

							
							
							
							
