													<h2>星云：达赖来的有三不对</h2>
						
						
																											<div id="postmessage_223102" class="t_msgfont">中新社高雄九月一日电 题：星云大师谈达赖访台等问题<br />
<br />
　　记者 董会峰 刘舒凌<br />
<br />
　　九月一日，即将迎来八十四岁生日的台湾宗教界领袖、佛光山开山宗长星云大师，在高雄县佛光山传灯楼接受了大陆媒体驻点记者联合采访。以下为部分答问内容。<br />
<br />
　　问：“莫拉克”台风引发的“八八水灾”，给台湾中南部民众带来巨大伤痛。面对数十年罕见的天灾，台湾各界纷纷赈灾，大陆也捐赠了救灾的善款及物资援助，令人想起去年四川汶川强震台湾同胞施援的情景。请星云大师就此谈谈感想。<br />
　答：上一次，四川发生大地震，面对灾情，两岸人民救灾不分彼此。这一次，台湾发生风灾，大陆的表现也难能可贵，官方、团体、寺庙、朋友纷纷赈灾，涌现出一些很感人的事迹。我觉得，灾难是不幸的，但也拉近了两岸人民的感情和交情，展现出对中华民族的信心、对未来的信心。<br />
<br />
　　问：请扼要介绍一下，佛光山在这次参与救灾、抚慰受灾民众心灵方面所做的工作。<br />
<br />
　　答：我们不是救灾的专家，只是从旁协助，尽一点心，不敢居功。这次风灾发生后，我就回来，向“内政部”转交大陆宗教界的捐款五百万人民币，我们自己也随即捐出一千万新台币。随后，佛光山发动义工数万人进入灾区，同时展开募捐，打开山门，先后收容二千多人，包括不少原住部落受灾民众、小朋友。做义工，这很好啊，尽一点人情、仁义。如此，这个社会就很可爱。<br />
<br />
　　问：星云大师深谙并不遗余力推动两岸交流，请谈谈对两岸交流及两岸关系和平发展的看法。<br />
<br />
　　答：我是江苏扬州人，在台湾六十年。两岸同胞都是中国人，如今中国已不是过去的“东亚病夫”，发展得很快，应该更包容、慈悲、关怀，不分境内外、两岸四地，加强你来我往，让大家一起分享身为中国人的光荣。<br />
<br />
　　我是八十多岁的人了，眼睛已看不清东西，希望能利用有限的时间，为两岸交流多奉献一点心力。<br />
<br />
　　问：在救灾重建工作如火如荼、新流感疫情蔓延之际，民进党七县市首长邀请达赖喇嘛访台，引起不少争议。请问星云大师，这次达赖访台，会对两岸关系产生怎样的影响？<br />
<br />
　　答：达赖喇嘛这次不应该来台湾。来了，宜淡处理。我认为，这次达赖到台湾来，对他不利，对民进党不利。<br />
<br />
　　第一，达赖是个宗教人士，要来台湾，理应由宗教界邀请。用政治人物、政治途径来邀请，他就变成政治人物了。而这次并不是宗教界邀请的。<br />
<br />
　　第二，这次灾情如此严重，达赖要来，应让救灾的地方来邀请。没有受灾的地方邀请他，就是宗教之外的政治操弄。<br />
<br />
　　第三，邀请程序不对。正因为程序不对、时间不对、地点不对，来了就会发生问题。我们希望对两岸关系不要有损失。<br />
<br />
　　是次采访，大约进行一个小时，星云大师有问必答。他还就吸取教训保育山水、共同弘扬中华文化、“人间佛教”理念、培养佛教人才等，发表了意见和建议。(完) <br />
