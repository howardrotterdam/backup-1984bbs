<br />
26日下午，在手术室门前，家属焦急地等待。对事发的原因，家属讳莫如深，而该学生的同学也推说不清楚情况。据了解，该学生姓陈，为长沙新华电脑学校的学生，今年19岁。 <br />
<br />
接诊陈某的医生陈志介绍说，26日凌晨2时许，陈某被送至医院。在治疗过程中，陈某同学隐瞒了陈某病情，只是称陈某酒醉，肛门出血。当时医生以为只是痔疮等问题引起的出血，就输液解酒对症进行了一番处理。 <br />
<br />
直至26日凌晨5点30分左右，陈某酒醒后腹痛难忍，医院紧急对陈某拍X片后发现其腹部有一异物，形似电视遥控器，而对其肛门检查发现肛管多处撕裂出血，陪同来的学生对事实真相讳莫如深。 <br />
<br />
究竟是什么原因，电视遥控器会被人插入19岁大学生肛门？其同学为什么对医生隐瞒病情？ <br />
