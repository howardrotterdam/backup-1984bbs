红网长沙11月27日讯（潇湘晨报滚动新闻记者 童辉）11月26日，湖南航天医院接受了一位19岁的受伤学生，其G门被人塞入18厘米长的电视遥控器，直肠被捅破。经过三个小时的手术，遥控器顺利取出。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp;&nbsp;&nbsp;“当时我就判断，这是有人在病人酒醉后不醒人事的情况下，用暴力将电视遥控器从G门X入，并刺破肠壁，进入腹腔内。”湖南航天医院接诊医生陈志称，如此行径实是骇人听闻，他就立即报了警。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp;&nbsp;&nbsp;据新华电脑学校一姓藏的负责人介绍，25日晚10时许，陈某前往涉外经济学院参加同学生日聚会。当晚酒醉后，不知与同学打什么赌，其同学便将电视遥控器塞入陈某G门。学校是次日早上才得知这一事情的。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp;&nbsp;&nbsp;26日手术后，一位同学向陈志介绍情况说，当日生日聚会喝酒后，他们便在涉外学院附近一宾馆开了两间房，陈某与另外两人住在一起。半夜一点多的时候，他们忽然敲门说陈某出事了，具体他们3人在里面发生了什么，他也搞不清楚。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp;&nbsp;&nbsp;航天医院方面表示，本着救死扶伤的人道主义精神，医院在病人身无分文的情况下，积极对病人进行救治，26日上午，在普外科医师和G肠科医师紧急会诊后，医院制定了详尽的手术方案，于11点20开始进行手术。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp;&nbsp;&nbsp;在全麻情况，医师进行剖腹探查术及肛管探查术后发现，陈某G门及G管多处撕裂，长达15厘米，遥控器捅破直肠与乙状结肠交界处，破口长达7厘米，电视遥控器位于右下腹腔，下方一小部分仍留在直肠内。经3小时的手术，医师取出了遥控器并修补了肠壁及G管。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp;&nbsp;&nbsp;陈志说，如今异物捅进G门的病人屡见不鲜，有灯泡、有萝卜，但像陈某这样的，实属罕见。 <br />
