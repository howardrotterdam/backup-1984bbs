													<h2>中国留学生：朝鲜是一个值得我们尊敬的国家</h2>
						
						
																											<div id="postmessage_302287" class="t_msgfont"><img src="http://news.sznews.com/images/attachement/jpg/site3/20091027/0019b90e16d60c50d40f00.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
陈默，北京第二外国语学院朝鲜语专业大四学生<br />
<img src="http://news.sznews.com/images/attachement/jpg/site3/20091027/0019b90e16d60c50d40f01.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
中国青年代表团成员在平壤友谊商店购物。本报记者 李建泉摄　<br />
深圳新闻网 讯&nbsp;&nbsp;10月2日，应朝鲜金日成社会主义青年同盟中央委员会的邀请，以共青团中央书记处第一书记陆昊为总团长的中国青年代表团一行100人，对朝鲜进行了为期6天的友好访问。北京第二外国语学院朝鲜语专业大四学生陈默，作为代表团成员之一，参加了这个活动。<br />
　　去年3月，陈默作为交换生，曾前往朝鲜金亨稷师范大学交流学习一学年。“朝鲜是一个值得我们尊敬的国家。”陈默说。<br />
　　中国青年报：去年你在朝鲜生活了9个月，过得怎么样啊？<br />
　陈默：国际上有一些媒体报道说，朝鲜人都吃不饱。在我出发前，不少朋友也为我担心，但结果恰恰相反，我在朝鲜的日子过得非常舒服，不但吃得饱，还吃得很好呢。学校食堂是免费的，饭菜很丰盛。我们留学生还经常在大学附近的餐馆聚餐。<br />
<br />
<br />
　　我住的留学生公寓离大学有20分钟车程，条件比我在北京的宿舍还好。在平壤，我们住的是七八个人一起的套间，每个房间住1到2人，几乎每个套间都会安排一名朝鲜大学生和我们同住，帮助我们学习朝鲜语。有个叫白明哲的男同学，对我们中国留学生非常好，我得到他许多帮助。<br />
<br />
<br />
　　中国青年报：那就聊聊白明哲？<br />
<br />
<br />
　　陈默：他来自朝鲜北部的一个城市，是哲学专业大四学生，今年31岁。在朝鲜，男性要有10年时间服兵役，所以很多男生读书时年龄比较大了。我曾问他什么时候考虑结婚。他说，一定要为祖国做出贡献后，才会考虑结婚。他指的就是工作之后再说。我还问过他对另一半的要求，他说，首先是要孝顺，然后是热爱祖国。<br />
<br />
<br />
　　记得白明哲和我第一次见面，就对我说“你长得真像售货员”，让我好困惑。后来才知道，在朝鲜，售货员是女性最向往的一个职业，相当于我们国家的高级女白领。那里80%的售货员都是大学毕业，长得也特别俊。隔壁宿舍的朝鲜女大学生就曾对我说，她的理想，就是做一名售货员。所以，白明哲说我长得像售货员，是在夸我漂亮。<br />
<br />
<br />
　　平壤交警可能算是世界上最漂亮的交警了，清一色都是女性，个个身材高挑，相貌不凡，跟我们的空姐似的。所以，如果家里出了一个交警，那是值得整个家族自豪的事情。<br />
<br />
<br />
　　中国青年报：白明哲他们的理想是什么呢？<br />
<br />
<br />
　　陈默：我问过他，他说想做图书管理员，或者到纪念馆做管理员。这应该算是朝鲜男性向往的职业吧。<br />
<br />
<br />
　　我感觉朝鲜青年对学习特别严谨认真。每当我问他们一个问题，他们都会很耐心地给我讲解。他们不懂的，还会马上去翻笔记。他们的笔记，那叫一个详细、工整！朝鲜的图书不太丰富，所以他们经常是靠抄来的笔记学习。你知道吗，在朝鲜，无论是路上的行人，还是在地铁里的人，几乎是人手一本笔记，无论男女老少，都在认真学习，这一点让我万分敬佩。<br />
中国青年报：你觉得朝鲜青年的精神状态怎么样？<br />
<br />
<br />
　　陈默：你问的这个问题太好了！你知道吗，我觉得朝鲜青年的精神风貌，是他们区别于其他国家青年的最大特点。他们说话声音洪亮，铿锵有力，走起路来像飞一样，而且个个神采飞扬，感觉他们永远都充满朝气。朝鲜男青年个个身材精悍，很有力量。他们一旦遇到困难，会很主动地去解决，从不退缩。比如白明哲在学习上遇到过挫折，我问他怎么办，他毫不犹豫地说：“我不会倒下的，为了国家，我要靠自己的努力去解决。”这是个有信仰的民族，他们的信仰就是为国家而努力，我想，他们正是有了这么个单纯的理想，才能时刻如此精神抖擞。<br />
<br />
<br />
　　这让我想起在朝鲜时看过的一部电视剧。电视剧中，一位中年将军说：“一个真正的青年，应该睡4个小时，工作20个小时。”他身边的几个青年听后都坚定地点点头。虽然那句话，在我们看来有点夸张了，但朝鲜青年真地就是这么去努力的。在这一点上，他们是我们的榜样。<br />
<br />
<br />
　　中国青年报：你的朝鲜老师如何？<br />
<br />
<br />
　　陈默：哎呀，他们才是真正的为人师表！我印象最深的是一个教我们朝鲜语发展历史的老师。他总是一丝不苟地讲解每个知识点，不会因为我们都是朝鲜语专业的优秀生而放松一点。每次下课，整个黑板都是他的板书。你知道吗，朝鲜大学老师的工资和门卫是一个水平，但他们不会因为这个降低教学水平和热情。我们中国留学生经常说：“这么优秀的老师，如果能来中国当我们的外教，那该多好啊！”<br />
<br />
<br />
　　中国青年报：朝鲜青年怎么看中国？<br />
<br />
<br />
　　陈默：你从他们的汉语水平就可见一斑。很多朝鲜青年都会说一点汉语，而且有些人的汉语水平让我们都惊叹。我去看过一场汉语比赛，整个过程，都令我们汗颜。坐在我旁边的一个中国学生不停地说，“他们的汉语，怎么说得比我还好啊！”真是一点都不夸张，他们的语音语调都像我们的播音员一样标准。反过来想想，我们这些朝鲜语高材生，跟他们比起来，真的差远了。<br />
<br />
<br />
　　朝鲜青年对中国的了解并不很多，不过他们一致认为，中国是他们最好的朋友，也是永远的朋友。在朝鲜，每当我们遇到困难，校方和同学都像是遇到天大的事一样重视，并很快地解决我们的问题。<br />
<br />
<br />
　　中国青年报：你将来的工作也许会经常和朝鲜人打交道，你会怎么面对他们呢？<br />
<br />
<br />
　　陈默：我们中国人从来讲“礼尚往来”，朝鲜人民对我们友好、敬重，我们自然也应该尊敬他们。再说，自立自强、热爱祖国的朝鲜人，本来就值得所有人尊敬。说句心里话，我很喜欢朝鲜人，他们单纯，积极向上。这次有幸加入访问朝鲜的中国青年代表团，作为分团的翻译之一，能向中国代表团成员介绍朝鲜的风土人情和历史文化，我感到很自豪。（本报记者 肖舒楠）<br />
