<br />
南韩联合参谋本部一名负责对北韩心战人士，谨慎响应传媒查询说，装设心战所用大型屏幕，需要几个月时间，现时还未决定是否将女子组合引入心战，「但是过去对北韩展开心战，也曾播出歌曲类节目，所以可能性还是有的」。<br />
<br />
心战人士强调，心战大屏幕将播放多元化的内容，不会只播女子组合的节目，至于利用女子组合打心战会达到怎样的效果，他也谨慎地表示自己并不十分清楚。<br />
