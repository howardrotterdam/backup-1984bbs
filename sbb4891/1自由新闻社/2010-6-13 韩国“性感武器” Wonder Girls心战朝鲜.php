韩战在 1953年休战，但从未签订正式和议，两韩 2004年关系缓和时停打心战。南韩上月重启心战，第一炮是在边境展开名为「自由之声」 FM电台广播，内容包括西方音乐、新闻和比较两韩状况，南韩国防部又打算用气球空投传单，讲天安号事件，并叫北韩士兵变节投奔南韩。<br />
<br />
北韩对于南韩打心战反应特别激烈，早前已警告要炮轰那些广播器，昨日军方又发声明，声称将以「全面军事打击」，摧毁边境所有南韩心战宣传设备，又恐吓指北韩军的报复行动，将令南韩首都首尔「变成火海」。<br />
