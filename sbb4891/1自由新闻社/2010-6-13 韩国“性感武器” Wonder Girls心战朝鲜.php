南韩指北韩鱼雷击沉巡逻舰「天安号」，造成 46死，上月宣布一系列报复行动，包括断绝经济关系和民间交流，更重启对北韩心战，其中一招是在边境 14处地点安装扬声器和 11块巨型电子显示板，向北韩士兵广播。<br />
<br />
震撼视觉打动军心<br />
<br />
据南韩《朝鲜日报》报道，心战广播并非过往的声带宣传广播，而是用上南韩当红女子组合的歌曲和音乐影片，包括少女时代、 Wonder Girls、 After School、 KARA和 4minute，以震撼视觉感官来打动北韩军心。<br />
<br />
五人组合 Wonder Girls的音乐影片，网上点击浏览次数超过 2亿次，少女时代九名美少女，各自通晓中、英、日语，在亚洲各国累积大量歌迷，其余女子组合的成员，也多是身材高？、打扮新潮的少女。<br />
<br />
外界认为，她们的大胆造型和性感衣着和舞姿，将对北韩士兵带来很大冲击，因为北韩那些穿传统服饰、只会千篇一律唱「亲爱的主席把我们照顾得无微不至」的艺人，无法跟她们相比，单是北韩士兵长期受共产教条主义熏陶，冲击有多大相信是难以估计。<br />
<br />
