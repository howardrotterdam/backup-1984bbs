<br />
「I want nobody nobody but you（啪啪）…… I want nobody nobody but you（啪啪）……」南韩女子组合 Wonder Girls一首《Nobody》，爆红半边天，无论歌词旋律到舞姿动作，都深入民心。南韩近年蔚然成风的美少女粉红兵团，亦将成为南韩对北韩发动心战的秘密武器，准备在边境大播她们的音乐影片，利用她们的性感打扮、劲歌热舞，融化北韩士兵冰冷的心。<br />
