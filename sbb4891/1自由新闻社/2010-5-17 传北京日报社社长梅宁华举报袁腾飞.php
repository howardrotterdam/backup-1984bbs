 <br />
&nbsp;&nbsp;发布时间：2010-05-17 17:26 文章来源： 作者： 苏然 网络编辑:刘京钊 <br />
这些天，历史教师袁腾飞的讲课引起网友热议，人们在激情争辩的时候，有人在网上突然抛出一个“大新闻”，说是警方准备拘传袁腾飞，而举报他的人正是北京日报社长，网络上还煞有其事的推出了举报信的全文。<br />
<br />
铺天盖地的矛头都对准人家社长，笔者觉得不公平，必须站出来讲几句话。其实仔细看过网上那些内容，稍有常识的人都会觉得十分可笑。北京日报社的社长怎么会去举报一个历史老师？那封举报信究竟是从哪里得到的？<br />
<br />
揭开这些事件的表面盖头，我们不难发现这其中蕴含的深刻内容。<strong>一些人对北京日报社近些年来在理论的大是大非问题上一直旗帜鲜明的立场感到非常难受，不惜用“小左”，倒退等字眼来形容他们的感受，恨不能把他们所谓的民主高高抬起。其实在理论问题上一直就有争论，孰是孰非早就有了定论。正是因为他们的失败恼羞成怒，才在有可能下手的地方进行报复。</strong><br />
<br />
从另一个角度看，这些人还有一个目的，袁腾飞的观点就好似他们的观点，一旦遭到抨击他们就想马上转换大家的注意力，抛出“举报信”来转移大家的视线，真可谓用心良苦。只是“机关算尽”，偷鸡不成反而蚀了米。<br />
<br />
实际上，<strong>即便是北京日报社长真的举报了袁腾飞也没什么大不了的。任何公民都有发表自己言论的自由，更可以发表不同意见和观点。这是中国的法律所保护的。笔者甚至认为，堂堂北京日报的社长，绝对可以公开发表自己的言论，而无须用匿名的方式。</strong><br />
<br />
从这件事情看出来，我们当下的网络环境需要大家共同来维护。虽然我们说网络是虚拟的世界，但这片虚拟世界也应当是一片净土。我们每一个网民要维护，绝不容任何人肆意毁谤，那种无中生有，胡编乱造的现象也不允许在网络上生存。笔者倒是建议，有良知的网友真的可以人肉搜索一下这些编造假话的人，把他们赶出我们的网络世界。<br />
