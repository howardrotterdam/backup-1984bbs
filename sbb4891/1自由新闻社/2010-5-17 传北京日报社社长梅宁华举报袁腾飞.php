													<h2>梅宁华曾经干过的事</h2>
						
						
																											<div id="postmessage_624503" class="t_msgfont">事情原委：<br />
<a href="http://snowyowls.blogbus.com/logs/5375869.html" target="_blank">http://snowyowls.blogbus.com/logs/5375869.html</a><br />
<br />
星期六晚上，接到了首都博物馆打来的电话，说我参加“我看博物馆”摄影大赛的照片《乌鸦与凤凰》得了个三等奖。这着实出乎我的意料。<br />
<br />
投稿的时候，真的没以为这张照片能够得奖，它表达得太直白了，尤其是在大赛组委会名单里还有梅宁华。对这个梅大人，我印象实在是太深了。2004年8月，当时还“负责报道一切”的新京报曾经就北京的旧城改造和风貌保护做过一期专题，那一期报纸上市之后，在当天中午之前销售一空。第二天，就是这位梅宁华，当时的北京文物局局长，带着北京电视台，北京电台，北京各大报纸的记者，在京城里一处一处跑，逐条反驳《新京报》的报道，指责他们“恶意炒作”。我至今仍然记得梅大局长一句经典台词:&quot;作为文保单位的梅兰芳故居只有护国寺附近一处，惟有 这处院子是被国务院认定的“梅兰芳故居”，以“梅兰芳故居”称呼的仅有此一处。梅兰芳故居保存完整，而且正在修缮之中。新京报造谣，炒作…… 但事实是，梅兰芳不仅在红星胡同住了将近20年，而且梅兰芳的四个孩子都出生在这里，梅派名剧《霸王别姬》《洛神》《西施》《春灯谜》都诞生在这里，印度诗人泰戈尔在内的世界各国文化名人也都曾是红星胡同梅兰芳故居的座上宾。就是这样一个文物局长，今天成了北京报业集团的社长，主持这场摄影比赛。没想到居然让我的那张照片成了漏网之鱼。<br />
<br />
周日晚上，我又接到首博打来的电话，工作人员征求我的意见，希望《乌鸦与凤凰》改一个名字展出。工作人员跟我解释说，这次大赛是518博物馆日的一个主题活动，届时会有很多领导来参加颁奖和作品展，《乌鸦与凤凰》这个名字刺激性太强，读者看到这个名字就会想，谁是乌鸦，谁是凤凰？作为一个官方举办的摄影比赛，首博不能这样公开地批评官方的城市规划政策，作为组织者，他们压力很大。<br />
<br />
这就是官方，自己做得，老百姓说不得。最后我同意改名了，《乌鸦与凤凰》变成了《一角之楼》 它曾经是紫禁城之一角，金碧辉煌傲视帝京，如今他辉煌依旧，却不得不蜷缩于城市的一角。<br />
<br />
刚刚又看到新京报关于东四八条拆迁的报道，不知道518当天那些大小官员们看到这一角之楼会作何感想。<br />
