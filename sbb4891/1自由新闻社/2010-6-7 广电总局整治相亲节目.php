2010年06月10日 14:42来源:东方今报<br />
<br />
“严禁伪造嘉宾身份，欺骗电视观众”、“不得选择社会形象不佳或有争议的人物担当主持人”、“不得以婚恋的名义对参与者进行羞辱或人身攻击，甚至讨论低俗涉性内容，不得展示和炒作拜金主义等不健康、不正确的婚恋观。”……<br />
<br />
昨日，国家广电总局正式下发了 《广电总局关于进一步规范婚恋交友类电视节目的管理通知》及《广电总局办公厅关于加强情感故事类电视节目管理的通知》两份正式文件，对已经引起极大社会公愤的 “相亲类节目泛滥、造假、低俗”等倾向着手整饬。文件中每一条都直指上述节目的软肋，可谓一针见血。今报记者随后也采访了社会各界人士，大家均对通知持赞成意见。<br />
<br />
“打假”是必须的<br />
<br />
恶劣现象：在当下的各大省级卫视中，《非诚勿扰》、《为爱向前冲》、《我们约会吧》等近10档名目各异的相亲类节目如火如荼，相亲类节目的“泛滥”已经让许多观众产生了“审美疲劳”。而更让观众质疑的，则是其中很多节目对嘉宾身份的“造假”行为。在国内各大门户网站及论坛上，网友将某些“明星嘉宾”的真实身份进行了揭露，引起了观众极大的愤慨。<br />
<br />
广电举措：审核嘉宾身份<br />
<br />
“婚恋交友类电视节目要把好嘉宾关，要认真核实嘉宾的真实身份，严禁伪造嘉宾身份，欺骗电视观众。”在广电总局昨日下发的文件中，对相亲类节目嘉宾造假现象十分鲜明地提出了“禁止令”。广电总局还建议此类节目要扩大嘉宾选择范围，不能仅由演员、模特、富二代占据荧屏。“不得邀请个人品德有问题或有争议，持不正确或非主流价值观、婚恋观的人物参加节目。”对嘉宾的个人身份审核，广电总局在通知中也有明确要求。<br />
<br />
社会反响：“广电总局这个要求十分到位，点到了这些节目的根儿上。确实要从嘉宾入手，才能从根本上杜绝造假等一切不良倾向。”湖南卫视节目中心工作人员李小姐告诉记者，他们也已经接到通知，并会严格按照要求执行。“相亲节目不是娱乐秀，不是电视剧，观众有权利看到真实的嘉宾表现，电视台没理由造假。”网络娱评人蒋晓莹说。<br />
<br />
“低俗”是严禁的<br />
<br />
恶劣现象：拜金女、富二代等“明星嘉宾”在相亲类节目中层出不穷，这引起了社会的极大反响。同时，此类节目中屡屡出现的嘉宾之间人身攻击、恶语相向，在营造高收视率的同时也引起了很多观众的反感。<br />
<br />
广电举措：不得现场直播<br />
<br />
“节目不得以婚恋的名义对参与者进行羞辱或人身攻击，甚至讨论低俗涉性内容，不得展示和炒作拜金主义等不健康、不正确的婚恋观。各婚恋节目不得在节目中互相诋毁、攻击。”广电总局在文件中明确指出，节目要有助于展现嘉宾健康向上的精神面貌和择偶观念，不得设计出位环节。广电总局还明文规定，所有交友类节目均不得现场直播，要严格执行播前审查和重播重审制度，对有问题的内容和错误的观点必须删除。<br />
<br />
社会反响：“现在热播相亲类节目的都是我国各大主流省级卫视，影响力都是很大的，一味地迎合低俗倾向会对很多观众造成不好的心理暗示。”我省心理学专家刘文清告诉记者，类似“拜金女”的流行，会让观众以为这就是社会的主流，产生极其不好的社会影响。<br />
<br />
“猎奇”是不当的<br />
<br />
恶劣现象：和相亲类节目的造假、低俗倾向一样，各种情感类节目的猎奇、炒作边缘题材等“审丑”行为，也引起了很大的社会反响。在日前举行的上海电视节上，央视名嘴王小丫也表示：“审丑不能成为制作方式上四两拨千斤的杠杆。把家庭矛盾、夫妻反目等放大化，我觉得是电视行业的悲哀。”<br />
