													<h2>《人民日报》连续发文狠批新版《红楼梦》</h2>
						
						
																											<div id="postmessage_759071" class="t_msgfont">《红楼梦》“走样”刺痛观众 名著重拍热的冷思考<br />
<br />
　　当下关于《西游记》、《三国演义》、《红楼梦》等文学名著重拍所引发的种种议论成为2010年度中国人文化生活的一道景观。“变形走样”和“不着边际”的重拍深深刺痛了观众的审美心理。这种“刺痛”绝非是文艺创造与创新所带来的冲击和震撼，而是胡乱的颠覆与无情的解构招致的“黄钟毁弃”与“瓦釜雷鸣”的时代之痛。<br />
　　<br />
　　　　重拍不应是浮泛的标新立异，而应是与时俱新的创造再生。真正具有创新价值的文艺作品一般都是平实中见奇崛、素朴中显气象，让人有新的发现与感悟；而刻意哗众取宠的所谓创新则徒有噱头，毫无营养，甚至引人颓废、让人消沉，这是创新与伪创新的基本界标。名著重拍是一项旨在创新的艺术工程，但是这种创新不是对原作的随意否定与肆意颠覆，也不是给原作的人物和情节穿上时髦的外衣，而是创作主体对原作的当代性阐释，对原作的独特感悟与精准演绎。成功的重拍是制作者的才识与原作者的智慧旗鼓相当的对话，是现作的精神内涵与原作的精神实质交相辉映的结果，不是一种简单的重现，更不是建立在陌生甚至误解基础上的“出新”。那些与当下庸俗观念沆瀣一气的重拍之作只是艺术长空中的闪电和惊雷，不会长久悬挂在历史的天空而辉耀千古。<br />
　　<br />
　　　　重拍不应单纯迎合，成为金钱的附庸，而应该坚守艺术的追求和理想。名著重拍中出现的多角恋、婚外恋等情感乱象；好人遭殃、笑贫不笑娼、小人得志等道德乱象；搞笑、愚乐、浅薄、狂妄与互相糟践等审美乱象实质上都是消费主义和资本逻辑在背后推波助澜的结果。譬如新版《红楼梦》中，厚重且寓意深刻的《好了歌》被当做调侃的对象一笑而过，则是对当下低俗之风的迎合。重拍作品与原作的核心精神和思想内涵构成了较大的反差，甚至以低俗的流行元素颠覆了原作的艺术精神，这是艺术俯就和谄媚收视率的典型体现。艺术没有了自己的锋芒与独特个性，没有了深度与力度，没有了感召力与向心力，在随波逐流中无原则地和观众嬉戏和狂欢，这样的艺术已经无法担负引领大众的使命与任务。我们不能苛求每一部名著重拍剧都拍成经典，但是我们决不能原谅众多的名著重拍如“苍蝇逐臭”般在荧屏前亵渎我们的民族经典。<br />
　　<br />
　　　　重拍应有传播民族优秀文化的担当，不应是肆意的精神放逐。名著重拍的初衷本应该是让民族优秀文化去烛照当代文化，用当代文化去激活民族优秀文化，既要守望优秀民族文化的精神家园，更要拥抱当代文化的新境界与新天地。无论是一个民族、还是一个国家，放弃了对优秀民族文化精神家园的坚守，丧失了传播民族优秀文化的勇气，其未来的发展就会像逐浪的浮萍，难以找到自己的根系。某些名著重拍对民族文化的肆意放逐，实际上反映了创作者艺术良心的普遍缺失，突显了其担当使命感的群体式微。缺乏担当意识的名著重拍其艺术生命一般不会走的很远。新版电视剧《三国》中对曹操的重新审视与公允评价值得肯定，冲破传统思维，能够以当代视野与时代情怀激活优秀民族文化。但是，该剧对民族忠信文化、道义文化、孝悌文化中精华元素的煽情化和泛情化处理、功利化和庸俗化演绎有待商榷，这种重拍策略一定程度上消解了原作中荡气回肠的艺术美感与文化气韵，让人观后怅然若失。名著重拍既应该反映和满足当代人的审美需要与情感诉求，更应该具有担当性与审美超越性，让观众既获得情感共鸣，又感受优秀民族文化的心灵荡涤，从而获得有品位的审美愉悦。<br />
　　<br />
　　　　我们欢迎充满创新意识与艺术超越精神的名著重拍，这样的重拍赋予原作新的时代内涵，也让民族艺术画廊更加五彩斑斓。而对那些媚俗且粗糙的“急就章”，那些没有艺术个性与担当使命、缺少艺术发现与审美超越的重拍，我们只有“敬而远之”！<br />
