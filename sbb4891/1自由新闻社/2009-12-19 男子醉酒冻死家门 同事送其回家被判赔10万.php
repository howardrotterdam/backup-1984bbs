													<h2>南京法官又有神奇判决：醉酒男冻死家门口 送其回家同事判赔10万</h2>
						
						
																											<div id="postmessage_384654" class="t_msgfont">醉酒男子冻死家门口 送其回家同事被判赔10万<br />
<a href="http://news.sina.com.cn/s/2009-12-19/032719292046.shtml" target="_blank">http://news.sina.com.cn/s/2009-12-19/032719292046.shtml</a><br />
 2009年12月19日03:27&nbsp;&nbsp;扬子晚报<br />
<br />
　　前天，网上一篇“我比彭宇还冤，送同事回家赔了10万”的帖子引起了众多网友的关注，发帖人是南京市民檀先生，他送醉酒同事朱培训回家，次日早上朱被发现冻死在家门口。南京市浦口法院一审判决檀先生赔付死者家属10.6万余元，檀先生不服，于上周上诉，并于昨天约见记者。南京中院的一名负责人透露，如果无意外，下月中旬将开庭再审此案。<br />
<br />
　　网上喊冤<br />
<br />
　　好心却落得赔钱下场<br />
<br />
　　前天，一名叫“公平、公正在哪里”的网友在西祠发帖，说今年2月19日，他和几个同事一起聚餐，大家都喝了些酒。结束后，一名姓朱的同事坚持要自己骑摩托车回家，但当天下雨路很不好走，他就喊了辆出租车送。车开到距离朱家约30米左右的巷口时，车子进不去。姓朱的同事就下车自己走了回去。<br />
<br />
　　他看对方神志清楚，“坚持不要我送”，而且离家门又近，就没多想，跟着出租车返回了。第二天上午，朱姓同事被发现死在离家门口不远的地方。一个月后，他被死者家属告上法庭。五个月后，浦口区法院以他护送、帮助义务未完全完成，判他付20%的责任，赔付10.6万余元。“我冤啊我！好心却落得如此下场！”<br />
<br />
　　网友举例<br />
<br />
　　国外有“好心人免责条例”<br />
<br />
　　对于这件事，网友的态度不再是“一边倒”。有的网友说，“好人不能做”。有的网友说，既然做了好事就要把事情做完，至少要打个电话给他家人，让他家人在门口接也好啊！网友“韶老张” 回帖说：如果你托朋友送一个小孩，你朋友把小孩送到了门口，小孩说我到家了，难道你朋友就可以转身走了？如果小孩没有进家门而是出去玩了，最后丢了！你觉得你朋友有责任吗？<br />
<br />
　　有一名网友举了国外的例子，来解决这种“两难”。加州的几个年轻人一起开车出游，其中一个女孩开车时遭遇了车祸，被卡在车里出不来，她的好朋友赶忙前来营救，最后女孩瘫痪了。女孩将好朋友告上法庭，说朋友拉她出车时用力过度，导致她瘫痪。加州议会司法委员会于2009年6月9日以5比0票通过 “好心人免责条例”，并提交议会表决，最终75比0票通过了“好心人免责条例”，该法的生效使得瘫痪女孩只能撤销起诉。<br />
<br />
　　约见记者<br />
<br />
　　我送人回家不是义务<br />
<br />
　　记者通过某种途径取得了发帖人檀先生的手机，他在电话里说话断断续续，明显有点走神，思想压力似乎也很大。经过一番简短的交流，檀先生终于提出，与记者当面交谈。见面时，檀先生带来了两份材料：法院判决书和尸检报告。他说话的时候，一直努力地试图冷静下来。但是，手一直微微地发抖，声音也有点哽咽。<br />
<br />
　　法院认为，檀先生作为一名醉酒者的护送人，不应该将其丢在家门口就走。“我怎么就成了护送人了？我只是送朱培训回家而已，我不存在任何义务。” 再者，檀先生说，自己到家后曾打电话给朱培训的妻子。但是，朱培训的妻子说，丈夫没回家，也找不到人。檀先生认为，对方的这种说法很值得怀疑。“警方曾经调查过周围的居民，当时周围动静很大，怎么可能找不到人呢？”<br />
<br />
　　此外，檀先生一直强调，朱培训很清醒。“他上车、下车都是自己走的。”不过，檀先生提到，他曾打电话叫了一个会开车的朋友送朱培训回家。途中，朱培训趁着酒劲向这个人倾诉了“家里的烦心事”，随后又以“手机掉座位下面了”为由下车，死活再也不肯上车。“我这才特地赶过去送他回家。”记者问檀先生，“如果朱是清醒的，会跟别人纠缠不休吗？”檀先生则认为他当时意识是清醒的。<br />
<br />
　　让人唏嘘不已的是，死者家属在今年2月19日悲剧发生后，曾向本报记者透露，朱培训被檀先生邀请当“参谋”，去江浦某小区看一套住房，晚上应檀先生邀请喝酒吃饭，这才发生了后面的悲剧。檀先生承认事情的过程，但坚持认为不是自己的责任。<br />
<br />
　　法院判决<br />
<br />
　　送人者应承担次要责任<br />
<br />
　　“朱培训是完全民事行为能力人，对饮酒的危害应该是很清楚的。对于自身死亡的后果，应承担主要的民事责任。而檀作为护送人员，在明知朱培训饮酒过多的情况下，其护送、帮助义务未能完成，从而导致朱培训在失去照顾的情况下丧失生命，也应承担相应的民事责任。”<br />
<br />
　　在浦口区法院的判决书中，最关键的就是以上这短短的100多字，表明了法官的基本态度。当然，最让檀先生不能接受的，就是高达10.6万元的赔偿。这其中还包括1万元的“精神损失费”。当初，死者家属要求的精神损害抚慰金2.5万元，法院认为过高，“酌定为1万元”。<br />
<br />
　　各方看法<br />
<br />
　　陪喝酒的人有间接责任。江苏圣典律师事务所的唐迎鸾律师认为，警方调查的死因很关键，朱是“醉酒后冻死”，而檀请朱喝酒，间接导致朱醉酒。当然陪他喝酒的人也有相应的责任，死者家属也可以将四人一起告上法庭。<br />
<br />
　　以后不敢送醉酒的人回家了。“如果法院维持原判，对社会良知将是一个很大的打击。以后没人敢送醉酒的人回家了。这等于是说，我不送你，什么事都没有。我送了你，只要不送进家门，你出了事，都算我的。”江苏行知律师事务所的律师葛勇武说，抛开良知不谈，即使从法律上的“义务”来说，也是有限度的。大家在一起喝酒，送对方回家，基本上是出于朋友感情，没有太多的“义务”可言。再加上对方行动自如，又能自己上下出租车，清醒程度比较高。按平时的情理和礼节，肯定只送到小区门口或者楼下。“对方都说了不用再送，你还要硬送，不难堪吗？”<br />
<br />
　　不能要求每个人都当奥特曼。“法院的判决，只会对死者家属是一个安慰，却伤害了大多数人。”南师大一名社会学家认为，送醉酒的朋友回家是一件挺微妙的事。送与不送之间，没有硬性规定，完全凭现场情况判断。送不送进家门，却是一个共识。“如果不是烂醉如泥，估计10个有9个都只会送到楼下。”而檀先生只是做了一个普通人应该做的事，却要因此付出不普通的代价。“这是他一个人的意外，法院一判决了，就会演变成所有人的分内事。你非要一个人去面对他完全无法预知的事，这就是要每个人都当奥特曼。”<br />
<br />
　　实习生 赵雯 本报记者 黄南</div>

							
							
							
							
