<br />
&nbsp; &nbsp; 刷盘子是“新内政” <br />
<br />
&nbsp; &nbsp; 今年1月告别白宫后，布希回到家乡过着普通人的生活。他说，现在在家常被夫人劳拉使唤做家务，劳拉告诉他，刷盘子是他的“新内政”。 <br />
