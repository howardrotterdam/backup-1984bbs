													<h2>画这个移民的饼是不行啦，仰融改想用股票圈钱了，真同情那签约的120个人啊。。。</h2>
						
						
																											<div id="postmessage_681400" class="t_msgfont">仰融团队北京融资 首只收购A股最快8月出水<br />
2010-06-12 12:17:19　来源: 经济观察报(北京)　跟贴 5 条 手机看股票 6月9日，美国正道汽车公司（HybridKineticMotorsCorp.）与美国投资移民中心ACFI（America'sCenterforForeignInvest－ments）在位于北京金宝街的励骏酒店就投资移民融资造车模式进行招商说明会。<br />
<br />
美国正道汽车公司正是曾经一手缔造华晨汽车的仰融回归产业、二次造车的载体，这次说明会是仰融宣布二次造车后，其团队和运作思路的首次公开亮相。<br />
<br />
在说明会上，仰融二次造车的技术、产品、融资模式等均被一一披露。不难看出，仰融正试图用一种新的资本与产业相结合的模式，再度重返整车制造业的舞台。<br />
<br />
正道汽车首席执行官王川涛在接受本报采访时透露，随着投资移民融资项目的启动，正道造车项目加速，其美国工厂即将启动，国内项目的运作也已经进入实操阶段，且仍是以资本运作切入。<br />
<br />
“如果一切顺利，年底前，我们就会完成对国内4家A股公司的收购谈判，目前已经有一家A股公司进入谈判尾声，并发布了提示性公告，很快我们在国内收购的首支A股就会浮出水面，成为我们切入国内造车的先导公司。”王川涛告诉本报记者。<br />
<br />
记者通过采访获悉，北京说明会并不是仰融团队此番回国的惟一目的。据消息人士透露，6月10日，王川涛等人还跟国内一家整车企业就合资合作进行了首轮正式谈判。<br />
<br />
国内运作加速<br />
<br />
“投资移民项目的启动，意味着正道造车项目已经进入实操阶段，开始加速。而通过近一年的国内考察运作，中国项目正在加快。”王川涛表示。<br />
<br />
此前，正道团队回国选址、成立股权投资管理基金公司、收购A股等事宜已经被先后披露。<br />
<br />
事实上，早在5月份时，就有消息人士向本报透露，仰融团队一直频繁地与国内多家A股企业进行接触，并与其中一家A股公司的谈判进展很快，“那个A股公司已经在5月底停过牌，发了提示性公告，有些股东已经接到了审慎调查方面的文件。”<br />
<br />
当时，记者曾试图透过上市公司的公告寻找出此家上市公司，但无功而返。上述消息人士曾解释，“收购方并非是仰融掌控的正道集团，也不是其在中国成立的天津正道股权投资管理基金公司等，而是一家另外的公司，而且被收购方也与汽车几乎没有任何关联，而是打算在收购后进行主营业务的变更。”但该人士拒绝透露这家A股公司的名字。<br />
<br />
对此，王川涛并未予以否认，他表示，“的确有一家A股公司的收购进展很快，目前正在走相关手续，但最终结果还要看主管部门的批复。”<br />
<br />
他也拒绝透露该A股公司名称，“这属于商业秘密范畴，尤其涉及到上市公司，但最快8月份就会有结果对外公布。”<br />
<br />
同时，王川涛透露，正道汽车在国内并非只与此一家A股公司接触，“我们的目标是年内争取完成对4家A股公司收购的谈判。正道要走的是一条资本与产业相结合的造车道路，肯定会紧密联系资本市场，并有效应用。”<br />
<br />
王还透露，在资本运作之外，正道在国内的选址和合资合作项目也在加快。“按照我们的时间表，最晚今年底就要确定国内三家工厂的选址，为后续的动工做准备。”<br />
<br />
但提及此前已经进行过多轮谈判，并赴美参加过正道汽车与意大利乔治亚罗设计公司签约仪式的天津、合肥、宁波三个地方政府，是否就是最终选址的时候，王川涛表示，“有些基本已经确定，有些还在考虑，后续加入的地方政府也很多，政策也更为优惠。”<br />
<br />
另外，对于消息人士称“王川涛等人即将赴某国内整车公司进行合资合作谈判”一说，王川涛也并未予以否认，但同样未透露该整车企业的名称。“这个是事实，正道在国内建厂，出于整车生产资质等考虑，与国内车企进行合资合作是不错的选择。”<br />
<br />
值得一提的是，最近一段时间，正道汽车母公司正道集团多次发出了收购浙江佳贝思电池公司的相关公告。最新的公告显示，正道集团已经与佳贝思签订了有关收购的第二份补充协议，将以人民币1.8亿元 （相当于2.0465亿港元）收购该公司。<br />
<br />
对此，王川涛表示，正道汽车以先进的混合动力汽车切入整车制造，电池是关键技术之一，通过多轮接触，正道认为该公司拥有一定的技术实力，于是作出了收购的决定。<br />
<br />
但同时，王川涛也留下了一句耐人寻味的话，“佳贝思只是正道产业布局的一颗棋子，不久的将来还会有大动作。”<br />
<br />
投资移民融资项目启动<br />
<br />
自仰融二次造车消息传出后，利用美国投资移民政策融资一事就备受关注。此番，王川涛携美国投资移民中心ACFI高管、美国阿拉巴马州政府官员等，就“正道汽车阿拉巴马项目”进行了详细的说明，并抛出了前期的优惠政策。当天会议的参与者，以媒体和中介机构为主。<br />
<br />
其中，阿拉巴马州政府官员的现身意在表达投资移民项目的合法性，并表示州政府对该项目分外重视，对此项目予以“巨大项目投资优惠”政策，先期已经给予了2.4万亩土地优惠。而美国投资移民中心ACFI高管则意在说明投资移民项目的透明性和便捷性，他表示，“项目和整个运作方式已经获得了美国政府的批准，本次说明会是取得有关移民美国问题正确答案的好机会。”<br />
<br />
王川涛则表示，与既往美国投资移民项目投资却难见回报不同，ACFI投资移民项目有了颇具前景的环保车新兴产业为基础，不仅可以让投资者拿到绿卡，更在一种极其透明模式下，让投资者成为先进环保车项目的股东，真实享受上市公司股权，成为真正的拿着绿卡的投资者。<br />
<br />
王解释，正道汽车已经进行了充分的前期运作，将投资项目与香港上市公司正道集团（1188.HK）挂钩，投资者不仅可以获得绿卡，还可以凭借投资额度获得相应的上市公司股票，保障稳定的收益。“以50万美金的投资为例，五年后投资者将拿到绿卡，同时拿到价值65万美金的上市公司股票，届时，投资者可以根据个人判断，抛售股票，或继续保留成为上市公司股东，分享未来的成功。”<br />
<br />
不过，仍有与会的媒体认为，“无论就造车项目本身还是投资移民融资的方式，都有些空和大。”<br />
<br />
但王川涛认为，汽车工业发展至今，再以传统模式切入显然不行，正道汽车在创立之初就对国际汽车制造业进行了充分的分析，以先进的节能、环保造车技术切入整车制造。主要致力于在混合动力汽车的研发、生产及销售领域取得突破。<br />
<br />
“正道汽车已经制定了务实的2030年以前产品开发三步走技术路线，涵盖了先进的混合动力技术、电动汽车技术，以及未来先进的纳米发动机技术。供应商、合作伙伴均是该领域中国际知名企业。团队成员更是囊括了有着多年在通用、福特、克莱斯勒、宝马等国际大型汽车公司工作经历的高端汽车人才，和一些资本运作方面的高手。”王川涛表示。<br />
<br />
对于投资移民融资方面的质疑，王川涛表示，“为了使投资移民融资项目的双方――投资者和正道汽车都能够得到有效的保障，正道为此进行了长达8个月的准备，邀请美国诺顿商学院对项目进行调研、评估，聘请了三个事务所进行修订，最终形成了完善的方案，于今年4月份递交的美国投资移民局进行审批，并在6月1日拿到了正式的批复。”<br />
<br />
王还强调，“正道汽车阿拉巴马项目已经获得了美国联邦政府的批准，是美国投资移民22年历史上，最大的项目，而且也是目前唯一一个与上市公司挂钩的项目。”<br />
<br />
<br />
不过，在接受本报采访时，王川涛透露，投资移民项目融资只是正道汽车四种融资方式之一。“除了投资移民方式，正道汽车的融资还包括中美两地的专业投资者、私募；正道汽车母公司正道集团（1188.HK）是在香港上市的上市公司；政府基金，尤其是美国政府对节能环保项目的基金。”<br />
<br />
按照正道的规划，投资移民项目两年内的融资规模要达到7000人，即35亿美金，融资将用于美国阿拉巴马州工厂的建设，以及部分产品开发费用。一旦该项目进展缓慢，正道将动用其他融资渠道先行建厂。<br />
<br />
在王看来，“正道阿拉巴马州工厂将作为一个示范性的绿色工厂模板，未来这个工厂将翻版到其他国家特别是中国，推动全球的绿色产业联动。”<br />
<br />
(本文来源：经济观察报 作者：王秋凤)</div>

							
							
							
							
