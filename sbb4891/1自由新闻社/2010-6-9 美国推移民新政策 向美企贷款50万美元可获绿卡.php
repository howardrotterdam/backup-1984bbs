仰融团队北京路演 推销其移民工业投资<br />
2010-06-09 08:33:40　来源: 21世纪经济报道(广州)　跟贴 0 条 手机看新闻 21世纪经济报道6月9日报道 6月9日，“正道汽车阿拉巴马项目”大型说明会在北京举行，这是备受关注的美国正道汽车公司联合美国投资移民中心，首次公开在中国路演。<br />
<br />
“这个项目是美国投资移民项目22年以来，最大的工业投资项目。”6月8日，专程从美国赶来的正道汽车总裁王川涛接受了本报独家采访。<br />
<br />
在王川涛看来，正道汽车阿拉巴马的项目具有独特的融资模式，通过国际融资，建立一个新的绿色工厂，这个绿色工厂可以翻版到其他国家特别是中国，并推动全球的绿色产业动起来。<br />
<br />
然而这一项目对普通投资者的吸引力何在？天津要成为其生产基地还存在哪些障碍？其首倡的混动技术真能超越丰田Prius？<br />
<br />
《21世纪》：正道汽车阿拉巴马项目最新进展怎样？<br />
<br />
王川涛：从产品开发到技术完善已经基本结束，目前要做的第一步是阿拉巴马项目开始建厂，开始建厂就意味着大部分资金需要投进去。目前除了一部分私墓资金之外，我们需要EB-5（Employment-based Fifth Preference，“第五类优先就业型移民签证”）项目大笔的资金到位。<br />
<br />
明天的这个活动，把这个项目向我们的投资者进行全面的阐述，我们的这个项目到底是个什么项目，车是什么车，面对面交谈。<br />
<br />
我们这个项目6月1日，正式得到联邦政府的批准，这是最大的进展。我们这个项目是美国投资移民项目22年以来，最大的工业投资项目，是新兴的绿色工业工程项目。<br />
<br />
EB-5与上市公司联系是专利<br />
<br />
《21世纪》：主要体现在融资手段方面的创新？<br />
<br />
王川涛：它牵扯到融资的方法与以往不同，我们把EB-5与金融市场结合在一起。具体来说，投资阿拉巴马项目5年之后，如果要退出这个投资，我们回给投资者65万等值的香港上市公司的股票，可以在股票市场兑现或继续持有。<br />
<br />
将EB-5项目与上市公司联系在一起，我们是第一家，已经申请了国际专利，这个手续很复杂，牵扯中国、美国，香港“联交所”的法律。为了完善这套法律，我们在三地请了三个律师事务所，2月12日，所有工作完善。4月21日，我们把第一个case送到移民局，6月1日被批准。<br />
<br />
《21世纪》：这对于你们来说意义重大？<br />
<br />
王川涛：这表明美国政府认可我们全部的投资移民的方式、方法、系统，特别是换股，把这50万投资换成65万股票的话要得到香港联交所的认可。现在基本上全部都完成，以后我们的投资者只要本人没有犯罪记录，他的钱是干净的，就可以通过移民局的快速通道在几个星期内审批下来，一般的审批要几个月，这也是我们这个项目在融资方面最大的进展。以后我们的项目，就不需要对整个过程进行审批了，而只需对投资人的资金进行审批，只要资金全部合法，就没问题了。<br />
<br />
《21世纪》：目前EB-5融资的情况如何？<br />
<br />
王川涛：目前有很多的投资者，在等待我们6月1日大门的打开。特别值得一提的是，EB-5项目不止限于中国，除了中国之外，还包括俄罗斯、印度、韩国和墨西哥。实际上，目前，墨西哥投资移民投到我们这个项目是最多的。<br />
<br />
《21世纪》：对于普通的投资者来说，这个项目最大的吸引力是什么？<br />
<br />
王川涛：首先，对私人投资者来说，这是很好的让子孙受教育的机会，可以延续个人的成功。第二，这个项目是受中美两国政府扶植的产业，节能环保是大势所趋。而以往的投资移民项目，大部分投资人基本没有回报，本金拿不回来，从投资角度来看，我们这个项目是美国投资移民项目设立未来相当长的时间，最有回报的项目。<br />
<br />
第三，我们的投资移民项目，看起来钱投在阿拉巴马，但是阿拉巴马项目的成功，它的经验和模式又会原封不动地搬回到中国，一次设计、一套图纸，同样的工厂，可以在中美两国翻版，我们在中国会有与美国同样的生产基地，这样一次投资受益两个国家。<br />
<br />
《21世纪》：造车项目需要大量的资金支持，公司具体的资金的计划和目标如何？<br />
<br />
王川涛：阿拉巴马项目第一期投资只需要16亿美金，那么3600个移民就够了，我们这个项目是受到美国各级政府特别的批准和支持，政府会提供资金支持，2万亩土地是给我们的，单独建厂只需要10多亿，根据我们的投资方案，一期的16亿美金目前没有问题了。<br />
<br />
天津、沈阳、安徽都可能成为基地<br />
<br />
《21世纪》：您曾经制定了2018年达到300万辆的目标，是否可行？<br />
<br />
王川涛：基本上保证2013年出车，可能提前。2018年，阿拉巴马只有100万辆，目前EB-5的融资只服务于阿拉巴马的汽车项目。我们初期的规划是，一期工程，美国有三个基地，每个基地30万的产能，中国也有几个基地，最后希望达到中美两国各100万辆的目标。<br />
<br />
《21世纪》：谈到中国基地，目前是否已经选定？<br />
<br />
王川涛：后来居上的很多，目前很难公布，过早公布对地方政府和投资者都不利。<br />
<br />
《21世纪》：此前正道汽车公司产品设计部总监曾到天津参加论坛，是否正道汽车将天津作为中国战略重点？<br />
<br />
王川涛：天津的可能性一直都存在，只是最后规模如何确定的问题。目前，天津、沈阳、安徽都有可能，每一个地方我们的进展不一，各地方都比较积极。<br />
<br />
对我们正道汽车来说，任何一个地方的选择，都有要求和条件。地理位置、产业基础、地方政府的态度都是我们考虑的因素。另外，我们对融资环境要求比较高，希望政府可以提供比较好的融资平台，比如可以提供比较好的A股公司可供我们挑选、并购，然后以这个A股公司为主体进行运作等等。<br />
<br />
当然，土地和物流条件也很重要，我们的这个项目是大进大出，对物流的要求比较高，我们的规划设计除了设计院之外，国际上大的物流公司也参与进来。目前，我们与地方政府和企业的谈判都在深化。<br />
<br />
《21世纪》：天津的进展如何？<br />
<br />
王川涛：从物流和产业基础的角度，我们比较满意。在融资方面有所进展，我们在天津已经设立了天津正道投资管理有限公司，这个公司的运营已经进入正轨。下一步，我们希望在融资平台上与地方政府达成一致，天津有很多很好的金融政策支持，有些是上海和深圳都不能做到的，但天津上市公司的质量不是最好的。<br />
<br />
目前我们也在等待天津政府关于物流方面的规划，因为我们在国内选的有些基地，要承担对美国工厂的零部件供货，国际物流对我们来说非常重要。总体上来讲，天津物流的条件和区域位置是可以的，我们很看重这个地方。<br />
<br />
《21世纪》：据了解，你们一直在寻找主机厂合作伙伴？<br />
<br />
王川涛：我们确实在选择，但现在不方便透露具体的合作伙伴，主要是顾及到会给合作方带来被动。主机厂对我们来说是非常重要的，与我们合作的前提是必须具备轿车生产资质，而且我们的产品可以比较容易地融入这个公司，并且所在地政府可以非常积极地支持我们的项目。<br />
<br />
核心竞争力在于五大创新<br />
<br />
《21世纪》：中国业内对正道汽车的项目不乏质疑之声，站在现在这个时间的截点，您如何回应这些质疑？<br />
<br />
王川涛：作为一个新兴的公司造车，按照传统的观念，我们做的产品不同，融资不同，我完全可以理解这些质疑。最近，我们做了一个很大的关于中国和美国汽车市场的调查，我们目前很明确我们做什么类型和尺寸的车对我们的成功最有利，相应的我们的产品开发也做了一定的调整，我们的产品出来一定是最受欢迎的产品。<br />
<br />
《21世纪》：刚才您谈到了，针对中美两国市场的研究，能否谈谈它的价值？<br />
<br />
王川涛：这个研究让我们的产品规划产生了重大调整，我们目前有8款产品，哪一款车先做对于我们来说至关重要。但是，我现在不方便透露具体的调整内容，这是我们核心机密。<br />
<br />
《21世纪》：通过这个研究，您对中国市场的了解产生了什么变化？<br />
<br />
王川涛：通过比较之后，我们更有信心，而且我们认为，我们的汽车，包括排量、型号等方面不会对中国本土品牌形成直接的压力，我们的竞争对手将是合资品牌汽车，我们要冲击合资企业垄断的市场。<br />
<br />
《21世纪》：你首先倡导的是混合动力汽车，但是，您应该注意到，丰田的混合动力汽车Prius在中国的销售情况并不理想。<br />
<br />
王川涛：我也不看好这款车，首先车小、加速慢、0-100公里加速需要14秒，我们的产品比他大、加速只需6秒，我们的车要做具备欧洲的技术、日本车的价格，而且价格会低。价格我们非常看重，很敏感。另外，我们具有所有的混合动力技术，三合一混合动力，包括气电混合、油电混合、也可以油气混合。<br />
<br />
《21世纪》：我们的纯电动车何时上市？<br />
<br />
王川涛：2013年上市的新车就包括纯电动车。<br />
<br />
《21世纪》：您觉得整个项目的核心竞争力是什么？<br />
<br />
王川涛：总结起来就是五个创新。第一是“三合一”的环保节能技术是一个创新，天然气为主、电池为辅助动力、汽油是紧急使用的辅助燃料。即使在使用汽油的时候，我们的产品100公里的油耗仅为5升，使用混合动力不超过2升，而且这个产品的排放也很低，1公里只排放60克二氧化碳，欧盟2012年的要求也不过是120克。<br />
<br />
<br />
第二个创新，高技术汽车的低成本制造。首先采用中美两个经济大平台，第二是大批量生产，一套设计上百万、环保节能汽车制造几万台是无法实现盈利的，零部件大批量共享也很重要。<br />
<br />
第三是销售方面的创新，举例来说，我们向消费者提供8万公里（5万英里）的免费天然气。<br />
<br />
第四，我们还有下一代的纯电动车技术，以纳米为动力的不过多依赖蓄电池的电动车技术，我们这个技术对电池的要求非常低，绕过电池安全性等等问题。<br />
<br />
第五个创新是融资，包括EB-5、私墓基金、股权基金，以及香港上市公司。我们的融资方法是现在的主机厂没有的。<br />
<br />
(本文来源：21世纪经济报道 作者：何芳)</div>

							
							
							
							
