													<h2>温总理，郑咏欣同学叫你回家吃饭……</h2>
						
						
																											<div id="postmessage_202527" class="t_msgfont">学生给温总理写信说自己在党和政府的关怀下生活得很幸福的，你肯定听过不少，比如：<a href="http://www.cnr.cn/hnfw/xy/200803/t20080313_504733191.html" target="_blank">http://www.cnr.cn/hnfw/xy/200803/t20080313_504733191.html</a><br />
<br />
温总理为了表示亲民，回信或勉励或祝福学生的你也肯定听过，比如：<a href="http://press.idoican.com.cn/detail/articles/20080827170A154/" target="_blank">http://press.idoican.com.cn/detail/articles/20080827170A154/</a><br />
<br />
当然，也有些生活在资本主义社会的不懂事的中学生，他们虽然也写信给总理，却不是说自己在党和国家的关怀下茁壮成长的。比如下面这个郑同学……<br />
――希望温总理不要回复此同学说：多难强身。<br />
<br />
希望温总理吃饭时顺便想一想，怎样用法理说服这位生活在水深火热的资本主义社会而不自知的同学……<br />
<br />
--------------------------------------------------------------------------------------------------------------<br />
<br />
<br />
<br />
为许志永老师给Wen Jiabao总理的公开信<br />
<br />
<br />
<br />
温总理：<br />
<br />
斗胆用这个标题，因为我现在的心境，与龙应台教授执笔写《请用文明来说服我──给Hu Jintao先生的公开信》时有类似的感受；龙教授的文章，温总理想必已经读过了。我虽然只是一个高中学生，对国事的了解丶对文字的掌控，当然不比龙教授，但仍希望能透过自己手上微弱的笔，表达对政府处理「公盟」及许志永先生的手法的不满。<br />
<br />
温总理，提起你的名字时，人们都会说你是平民总理，办事以民为本，站在人民利益那方。有些小朋友还会被你的亲民作风所吸引，叫你一声温爷爷，视你为模仿的对象。<br />
<br />
但是当我一想起你任内被捕丶被禁丶被整顿的媒体和异见人士，如刘晓波先生丶程翔先生丶《冰点》杂志丶《南方都市报》等等时，我却又不得不质疑你作为纯真善良小朋友学习对象的资格！难道我们国家的教育，是要教小朋友与其他人意见不合时就要对付对方，而非讲求中国人千百年来坚持的仁义观？<br />
<br />
近日被政府盯中的是许志永先生和他所领导的「公盟」。<br />
<br />
公盟是由一群关注中国发展的律师及学者所组成的民间组织，他们透过学术研究就国家的法制改革提出一些意见和建议，推动国家实现民主法治。他们另一项为国人所熟知的工作，是为一些弱势群众如上访者丶被徵地者丶毒奶粉案的受害者等等提供法律援助，帮助他们透过现有的司法制度去取得公义。单从近日税局搜查後曾获得帮助的民众纷纷勇敢地到公盟办事处声援一事，任谁也看到「公盟」是站在人民那边的！为何温总理你所领导的政府仍要做出这件不合民情的事呢？ <br />
据我所知，「公盟」的工作是非牟利的，他们曾经想登记为民办非企业单位，但遭到当局拒绝，被迫申请为有限公司。在国际社会，这种团体并不需交税，而其捐献者更能获得免税优惠。但由於公盟的成员是守法的律师，明知制度的不合理仍旧依规定纳税。在被税局指控漏报税项时，亦坦诚地承认错误。为何温总理你所领导的政府仍要向他们徵收最高的罚款，并过分地在公盟办事处以「搜证」为名而检走所有维权资料呢？更令人感到无法理解的是，许志永先生突然在召开第二轮听证会之前，遭公安与便衣从家中带走，并扣留在看守所中，不能与家人及律师联络，同时更要公盟关闭其网页，这实在是对公民基本权利的无理剥夺。 <br />
温总理，你经常说要「依法执政」丶「依法治国」，我想请问你一下，执法机关是根据哪一条法例去带走许先生的？ <br />
<br />
我对中国法律的认识十分肤浅，但仍知道宪法是国家的根本大法，具有最高的法律效力。在我国宪法的第 35条列明，中华人民共和国公民有言论丶结社的自由，第 37条更清楚指出中华人民共和国公民的人身自由不受侵犯，禁止非法拘禁和以其他方法非法剥夺或者限制公民的人身自由。就我对上述条文的理解，我认为许先生现在应该可以自由地留在家或身处办公室办事的。<br />
<br />
今年 4月，我和其他同学到北京考察交流时，有幸在未被搜查的公盟办事处与许先生谈论中国政治。看见他愿意无私地为中国在法治民主领域上努力，并对於中国的未来充满了希望，令我深受感动。犹记得考察时，我们曾到过永定门内国务院信访办的门前，亲眼目睹不少恶形恶相的截访者和情况可怜的上访者。因此，在倾谈中有同学便问了许先生一句「为何中央政府会容忍那些截访者存在呢？」你知道许先生怎样答吗？他说上访人数远远超过信访部门所能承受，所以中央政府亦唯有容许截访者存在，以免信访部门的工作量极严重超标。在访问中他多次提醒我们中国政府已很努力，要对政府有多点耐性。<br />
<br />
许志永先生就是这样的一个人！他充满理想但不狂妄，他看到很远的目标仍坚持稳重地一步一步走下去，他不怕只有一点一点微弱的力量在慢慢地付出，坚信中国终有一天能实现法治民主与自由。<br />
<br />
温总理，我真的十分不解为何你们的心如此的狠。为何要用这方法去对付这样一个体谅政府丶理性论政的学者呢？他所做的事只是在现有的游戏规则下安分守己地为弱者去争取宪法赋予的权利。他做的事情无一不是爱国为民！为何中央连这样的一个人物也不能放过呢？为何不容许他和公盟透过公开公正的司法程序去处理这事呢？<br />
<br />
温总理，看着你为四川地震灾後工作努力丶关心矿工工作环境之时，我总想叫你一声「温爷爷」的。但当看着这麽多不合法不合理的事情在中国发生，我实在叫不出呀！但愿有一天这样的事情能够圆满解决并不再发生，我相信那一天海内外同胞才会由衷地振臂一呼「中国万岁」的！ <br />
<br />
祝 <br />
身体健康 <br />
香港中七学生 <br />
郑咏欣敬上</div>

							
							
							
							
