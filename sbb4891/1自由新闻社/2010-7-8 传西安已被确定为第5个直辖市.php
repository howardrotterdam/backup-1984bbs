													<h2>甘肃日报社评论，称西安已确定为第五个直辖市</h2>
						
						
																											<div id="postmessage_727496" class="t_msgfont">导读：甘肃省委机关报甘肃日报今日在第三版刊发题为《甘肃、兰州西部大开发10年节点再定位》的解读文章，文中提及“西安已被确定为国家第五个直辖市”。<br />
<br />
每日甘肃网-甘肃日报7月7日报道 站在西部大开发10年的节点上，我们沉浸于西部巨变带来欢愉的同时，同样接纳着理性的敲打。面对改革开放30年后中国经济增长模式的转型，面临新的10年征程，在中国未来发展的路上，甘肃、兰州如何找到自己的定位？北京财智经济战略研究院院长、著名战略咨询专家王志刚今天为我们带来了他的声音。<br />
<br />
　　不要让天水成为一块“骨头”<br />
<br />
　　关中―天水国家经济区已经国务院批准设立，这将是天水乃至甘肃发展的新契机。不过，王志刚道出了自己的担心。他说，根据规划，经济区大部分区域在陕西境内，陕西无疑是关天经济区的主体。对此，甘肃人一定要摆正自己的位置，一定要舍得把天水主动融入陕西，将来不要弄成陕西部分发展迅速、天水发展滞后的尴尬景况，导致天水成为一块“骨头”。<br />
<br />
　　要知道，国家规划的3个国际化中心城市中，就有西安，而且，西安已被确定为国家第五个直辖市。这更能够印证一个命题，那就是，天水、甘肃融入陕西后，将会得到更多的发展机遇。反之，则很可怕――西安、陕西面朝东部发展时，天水、甘肃就会成为少人问津的背部，经济盲区也会由此产生。<br />
<br />
　　清凉也是一个商机<br />
<br />
　　当下，东南沿海地区正处在“水深火热”之中，骄阳、洪水，已经令许多经济发达地区的城市居民头疼不堪。王志刚说，相对于东南沿海城市，甘肃有着“清凉夏宫”之美誉。甘肃不光要卖矿石和能源，卖白兰瓜和黑瓜子，也要卖“天气”，卖清凉。清凉的天气有时也是一种商机。<br />
<br />
　　王志刚说，东南沿海多为发达地区，有钱人居多，却常年饱受着炎热、洪水之害。而兰州、甘肃有着从5月份到10月份长达半年的清凉，有绝对的天气优势。“兰州、甘肃不妨打出‘清凉夏宫’的招牌，吸引东南沿海的消费者度夏消暑。”<br />
<br />
　　甘肃也有自己的“赵本山”、“杨丽萍”<br />
<br />
　　敦煌、《读者》都是甘肃的名片，更是全国绝版文化，吸引着众多海内外人士光顾和欣赏。如何将此类文化产品延展为产业化链条？<br />
<br />
　　王志刚分析说，赵本山是一种文化现象，一个人带活了一个东北；杨丽萍的孔雀舞，则把云南推向了世界。敦煌、《读者》就是甘肃的“赵本山”、“杨丽萍”，甘肃要像尊重赵本山、杨丽萍那样看待敦煌、《读者》，要让敦煌、《读者》当成文化的使者，带头出彩，从而带动相关文化产业随后跟上。</div>

							
							
							
							
