<br />
　　武汉大学周恩来旧居位于珞珈山南坡，为一栋坐北朝南西式二层楼房。据武汉地方志专家董玉梅介绍，这栋楼房不仅与一代伟人周恩来联系在一起，还是国共合作全民抗战的重要见证。<br />
<br />
　　1938年，武汉成为全国抗战的中心，武大校区成为国共军政要员居住之地。时任中共代表团负责人的周恩来于当年与夫人邓颖超住进这栋小楼，开展抗日民族统一战线工作，推动全民全面抗战的实现，还在此会见了埃德加・斯诺、史沫特莱等国际友人。因而这栋小楼又有“国共合作抗日小客厅”之誉，1983 年被武汉市政府列为文物保护单位。<br />
<br />
　　日前童汉芳与董玉梅踏访此地，发现昔日的周恩来旧居已残破不堪，无人问津，小楼大门口一片废墟，门前一块写有“武汉市文物保护单位，周恩来同志旧居”等字样的铭牌尚在。<br />
<br />
　　据了解，解放后周恩来旧居一直有人居住和使用。两年前，原居住户从此处搬出后，房子便空置下来。<br />
<br />
　　据悉，武汉大学有关部门早有意修缮维护此楼，但迟迟未见动工。<br />
<br />
　　众网友呼吁，不能让这栋有重要历史价值的建筑任其破败下去！<br />
