<br />
欧巴马是去年的和平奖得主，他表示，去年他注意到过去获这项殊荣的许多人士都比他作出更多牺牲，现在这个名单增加刘晓波；他为了自己的信仰牺牲自由，经由颁发和平奖给刘晓波，诺贝尔委员会选择了一位勇于发声及有勇气的人，刘晓波一直透过和平及非暴力方式推展普世价值，包括支持民主、人权及法治。<br />
