（美联社） <br />
大陆民运人士刘晓波获得诺贝尔和平奖，美国总统欧巴马发表声明表示欢迎，强调诺贝尔委员会选择了一位勇于发声与有勇气的得主，同时要求中国政府尊重人权，尽速释放刘晓波。<br />
