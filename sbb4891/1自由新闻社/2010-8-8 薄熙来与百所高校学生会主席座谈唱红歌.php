													<h2>薄熙来与百所高校学生会主席座谈唱红歌</h2>
						
						
																											<div id="postmessage_774514" class="t_msgfont">薄熙来与百位高校学生会主席座谈唱红歌(图)<br />
2010-08-08 02:59:49　来源: 重庆晚报(重庆)<br />
核心提示：日前，重庆市委书记薄熙来与北京大学等109所全国知名高校的学生会主席进行了座谈交流。期间薄熙来回答了有关重庆“打黑除恶”、“唱读讲传”等活动的问题，并与同学们一起高唱《歌唱祖国》。<br />
<br />
薄熙来会见“中国大学生骨干培养学校”第三期学员<br />
<br />
<br />
重庆晚报8月8日报道 4日上午，渝州宾馆新俱乐部会议室气氛热烈，市委书记薄熙来，市领导张轩、何事忠、马正其、陈存根坐在同学们中间，与参加“中国大学生骨干培养学校”的162名大学生亲切交流。团中央书记处书记卢雍政主持座谈会。<br />
<br />
这些大学生是北京大学、清华大学等109所全国知名高校的学生会或研究生会的主席。<br />
<br />
座谈会上，卢雍政首先介绍了“中国大学生骨干培养学校”第三期学员重庆实践锻炼活动的情况。自7月22日起，大学生们在重庆听报告，参观考察，并深入南川区8个乡镇与农民同吃、同住、同劳动，还举办登山比赛，收获颇丰，感触良多。<br />
<br />
高尚的精神和真挚的情感，是内生的、持久的动力，最能感染人、鼓舞人，而事在人为，只要人心齐，就事无不成<br />
<br />
座谈会上，薄熙来鼓励大家畅所欲言，提出的问题“有挑战性更好”！大学生们踊跃发问，气氛更加火热。<br />
<br />
中国社科院研究生会主席吴撼地抢先发言。她说，重庆唱红歌、读经典、讲故事、传箴言，让人斗志昂扬，想听听下一步的打算。<br />
<br />
薄熙来说，“唱读讲传”在重庆开展以来，干部群众积极性很高，一浪高过一浪，很有感染力。刚开始，我们只是提倡大家唱红歌，选定了45首歌曲，现在已经大大超出了这个范围。走在大街上，老大妈、小学生都能唱上几首。现在又读经典，讲故事，传箴言。一个地方的发展，离不开精气神，高尚的精神和真挚的情感，能够凝聚人心，形成强大的精神力量，推动社会的进步。“唱读讲传”就极大地推动了重庆的经济发展、社会进步，是内生的、持久的动力，最能感染人、鼓舞人，而事在人为，只要人心齐，就事无不成。<br />
<br />
有人表扬我们“打黑除恶”有胆识、有策略，其实，我们的策略就是“不讲”策略，直来直去，依法办案<br />
<br />
抢到话筒的清华大学学生会主席张昭遂说：“重庆市委、市政府决策坚决，顶住各种压力，打黑出重拳，除恶保平安，有胆识，有策略，大快人心，也振奋人心。我想问薄书记，您当初作决策时是怎样考虑的？”<br />
<br />
薄熙来说，讲到“打黑除恶”，老百姓感到大快人心，但也有人不舒服，不光是黑恶势力本身，还有与他们相关的那些人―――腐败分子和保护伞，因此会有不同的舆论和反应。但为了人民的利益，压力再大，也要勇敢面对，一定要将斗争进行到底，最重要的是政治决心。如果东张西望，想三想四，患得患失，就什么也不要做了。有人表扬我们“打黑除恶”有胆识、有策略，其实，我们的策略就是“不讲”策略，直来直去，依法办案，该怎么办就怎么办，任凭别人怎么说，也要大胆地往前走，坚决将犯罪分子绳之以法，绝不含糊。<br />
<br />
发展经济和改善民生并行不悖，改善民生的过程，就是发展经济的过程<br />
<br />
薄熙来说，刚才同学们还提到“民生十条”，涉及公租房，农民工户籍制度改革，农民养老，130万农村留守儿童，农民“万元增收”，校园安全，群众看病等问题，都是关乎老百姓安身立命的事情。这是重庆市委全会做出的一个重要决定，也是我们发展理念的重大转变。<br />
<br />
传统的思路是，先发展经济挣钱，有了钱再改善民生。现在的想法是，发展经济和改善民生并行不悖，改善民生的过程，就是发展经济的过程。驱动经济有“三驾马车”：投资、出口和消费。改善民生就是直接促进消费，拉动内需。如果说重庆发展与过去的路子有什么不同，就是一边促进消费、拉动内需，一边营造内地大市场，把经济发展起来。所以这次出台的“民生十条”，既是改善民生的迫切需要，也是探索以民生为导向的重庆发展经济之路。<br />
<br />
只要健康的、正义的力量团结起来，腐朽的势力就成了纸老虎，不堪一击<br />
<br />
复旦大学学生会主席李汇东好不容易抢到话筒，问了一个很多同学关心的问题：“薄书记在‘打黑除恶’过程中，在最困难的时候，比如说‘李庄案’的时候怎么想，能否跟我们分享一下您的心路历程？”<br />
<br />
薄熙来说，在我心里，李庄那个事情是个“小浪头”，现在还有一些“浪头”。总有一些人希望借“打黑除恶”表现一下，时不时捅点东西出来，我们是有思想准备的。“打黑除恶”涉及到那么多黑恶势力，打疼了那么多人，连带那么多利益集团，能没有矛盾吗？启动这场斗争的时候，我们就做好了迎接挑战的准备。<br />
<br />
薄熙来说，“打黑除恶”斗争中，来自市民的上万封举报信，80%是实名举报，这本身就说明了人民群众的态度。只要健康的、正义的力量团结起来，腐朽的势力就成了纸老虎，不堪一击。<br />
<br />
思想政治是最生动、最鲜活的东西，最能激发人内心的情感，更应入脑入心<br />
<br />
“对思想政治课，不少大学生不‘感冒’，逃课的人很多，场面冷冷清清。”天津大学研究生会主席邹D的发言引来一阵热议。<br />
<br />
薄熙来说，思想政治其实和人们的社会生活联系得最紧密，是最生动、最鲜活的东西，也最能激发人内心的情感，更应入脑入心。思想政治的教材并不只在课本上，重庆卫视的《记忆》、《英雄交响》栏目就很不错，今天播的一个节目就很能打动人，讲当年研制“两弹一星”的团队，原子弹、氢弹实验成功了，举国欢庆，而这些团队功臣们每个人只发了10元钱的奖金，大家根本不谈价钱，只讲奉献，都感到无上光荣！那时的人，就是一心报国，真让人感动！这也是一种思想教育，是入脑入心的政治。<br />
<br />
重庆为什么提倡红歌？就因为红歌唱响了理想信念，抒发了爱国主义，能让男女老少都感受到精神的力量<br />
<br />
重庆的社会实践，尤其是“三进三同”活动，让华中科技大学学生会主席马龙鹏触动很大，他剖析当代青年有知识、有能力，也愿意去奋斗，但比较现实和功利，关注自己多，关心集体少，遇到问题不愿承担责任，应该到基层接受锻炼。<br />
<br />
薄熙来说，你说的比较实际。与五十、六十年代相比，当代年轻人受到的教育更系统，知识结构也更完善，但有些人比较功利，思想比较脆弱，吃苦的精神头儿，集体主义的意识相对差一些，这也是一种个人主义、自由主义的表现。中国几千年文化深受孔孟之道影响，就是进入革命队伍的一些人也往往如此，遇到矛盾“大事化小，小事化了”，“见人只说三分话”……实用主义、功利主义思想严重。<br />
<br />
薄熙来说，一个伟大的民族要有凝聚力，要有奋斗的精神。抗战时期，无数中华儿女冒着敌人的炮火，“把我们的血肉，筑成新的长城！”胜利之后怎么办？还要不要奋斗？如果各奔东西，一盘散沙，是没有前途的，甚至是危险的。重庆为什么提倡红歌？就因为红歌唱响了理想信念，抒发了爱国主义，能让男女老少都感受到精神的力量。<br />
<br />
人民群众怎么看我们的接班人，其实就看能不能和他们心连心，看他们对工农的态度<br />
<br />
听完大家的发言和提问，薄熙来说，团中央组织全国大学生骨干，深入田间地头，了解农村这个中国最大的国情，很值得称道。<br />
<br />
薄熙来说，密切联系群众是我们党的光荣传统，毛主席说过：“看一个青年是不是革命的，拿什么做标准呢？拿什么去辨别他呢？只有一个标准，这就是看他愿意不愿意、并且实行不实行和广大的工农群众结合在一块。愿意并且实行和工农结合的，是革命的，否则就是不革命的”。如果我们这些学生的领头人，只是闷头读书，忙于考硕士、博士，只顾设计自己，和工农群众缺乏联系和感情，那就脱离了我们党所推动的事业。<br />
<br />
薄熙来说，我们的接班人必须和工农大众相结合，这是一个重大原则问题。人民群众怎么看我们的接班人，其实就看能不能和他们心连心，看他们对工农的态度。人民群众是最朴实，最实际的，如果年轻一代能真心实意地关心、爱护和尊重百姓，能把党和政府的温暖送到人民心间，他们就会发自内心地热爱党，就会感到我们的党、团组织是亲人。<br />
<br />
薄熙来说，重庆组织实施“三进三同”、“结穷亲”、“大下访”和“三项制度”，就是为了让广大干部密切联系群众，了解社情民意。知国情是年轻人做好工作的重要基础，仅从书本是不能了解国情的，必须亲身感受。谁走得勤，看得清，问得细，记得牢，谁才能真知、详知，才能对中国社会有更大的发言权。<br />
<br />
讲话，写文章，都要朴实自然，力戒空话、套话，如果老百姓听不懂，双方的距离就远了<br />
<br />
薄熙来希望大学生加强学习。他说，现在信息越来越多，不少人每天上网，看电视，读报纸，占用了大量时间，一天到晚也忙得很，但这并不等于学习，“走马观花”而已。静下心来想一想，未必能学到什么东西。一定要好好读点经典，才能汲取真正的“营养”。大家看马克思的文章，以其理论的深刻彻底和强大的逻辑力量使人信服。列宁的文章很有激情，读起来朗朗上口，很多堪称格言警句。毛主席的《矛盾论》、《实践论》等名篇，深入浅出，旁征博引，贯通古今，发人深思。这些伟人的作品确是经典，不可不读；还有李杜诗文，古典名著，年轻时多读一些，今后用起来就方便了。<br />
<br />
薄熙来说，各位同学发言，讲得很生动，很动情。希望大家讲话，写文章，都要朴实自然，力戒空话、套话，如果老百姓听不懂，双方的距离就远了。我们共产党、共青团应该是最能联系实际的，同学们多与群众打交道，语言风格和习惯也会潜移默化地受影响，你讲的话老百姓爱听，才有开展工作的条件。<br />
<br />
马克思主义、毛泽东思想、邓小平理论都是科学，是最伟大、最生动的理论，具有强大的生命力<br />
<br />
座谈中，有同学讲，这个团队还特意穿上了红色的T恤衫，因为我们是红色的接班人。薄熙来称赞说，大家是共青团嘛，就是要坚持我们的理想信念！红色是我们国旗的颜色，我们就是要“唱红”，就是要举红旗。现在有些人有意回避红色文化，好象宣传共产党的理论和学说就显得“左”或者“土”。其实，马克思主义、毛泽东思想、邓小平理论都是科学，是最伟大、最生动的理论，具有强大的生命力和蓬勃生机，而且潜力还大得很，可以引领中国走向更大的辉煌。我们搞改革开放，绝不是抛弃原来的理想信念，邓小平理论、“三个代表”重要思想和科学发展观都是在坚持和发展马列主义、毛泽东思想。<br />
<br />
能为大多数人带来幸福的人才是真正幸福的人<br />
<br />
<br />
薄熙来希望，青年人要有高尚的精神追求。我们讲，“德智体全面发展”，要“又红又专”，就是把精神因素摆在第一位。人到底为什么而活着？为吃得好吗？吃太好了还容易高血脂，得肥胖症。为挣大钱吗？钱财乃身外之物，生不带来、死不带去。留一笔钱给孩子吗？那是让孩子坐享其成，失去创业的渴望，到头来一事无成，害了他们！攒钱修个祠堂光宗耀祖吗？老百姓怎么会去敬那些大财主呢？把人生想彻底，还是马克思说得对，能为大多数人带来幸福的人才是真正幸福的人！<br />
<br />
薄熙来说，中国现在的经济蛋糕已经做大了，但还要有健康的精神生活。发展经济是硬道理，提升精神生活的质量也是硬道理。这次送给同学们一套光盘，一部《记忆》，一部《英雄交响》，专门讲述上世纪五十、六十年代的故事。那时的人们，生活很清苦，但大多数人都有一种发自内心的自豪感。当代大学生要从校园做起，传承和发扬高尚的、脱离了低级趣味的精神风貌，并进而影响当代中国的青年生活。<br />
<br />
同学们以热烈的掌声回应薄书记的讲话，并高唱《歌唱祖国》，大家全情投入，久久不愿离去……<br />
<br />
