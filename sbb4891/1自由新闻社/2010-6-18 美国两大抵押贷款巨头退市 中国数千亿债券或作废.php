													<h2>两房退市套牢债主 中方持有总额超3400亿美元</h2>
						
						
																											<div id="postmessage_709667" class="t_msgfont">时间：2010年06月29日 08:40:13 中财网 &nbsp; &nbsp; &nbsp; &nbsp; <br />
　　&quot;两房&quot;套牢中国债主<br />
　　伯南克在国会山上的证言，道出了&quot;两房&quot;退市的根源，他们担心经济二次探底。可大量持有&quot;两房&quot;债券的中国人，将来更多时候看到的是账面资产，想套现已经很难了。<br />
　　6月16日，&quot;两房&quot;宣布退市。<br />
　　在美国就业形势不容乐观、财政赤字越来越严重的情况下，奥巴马政府面对脆弱的房地产市场，已经没钱再救&quot;两房&quot;，美国国会议员甚至提出将&quot;两房&quot;列入可清算的金融实体。&quot;两房&quot;想咸鱼翻身的机会已经没有了。<br />
　　退市后的&quot;两房&quot;将进入场外交易平台，流动性大大降低。&quot;两房&quot;之前发行的5.2万亿美元债券，其价值进一步缩水，债券投资现正遭遇2008年金融危机之后的二次打击。<br />
　　中国金融机构持有&quot;两房&quot;衍生债券将近5000亿美元，&quot;两房&quot;退市后，中国金融机构抛售&quot;两房&quot;债券变得越来越困难。尽管中国政府呼吁美国政府要确保&quot;两房&quot;债券的安全，可美国政府已经力不从心，中国债主已被&quot;两房&quot;债券套牢。<br />
　　难以救助的&quot;两房&quot;？<br />
　　6月16日，美国两大房地产抵押贷款巨头房地美(Freddie Mac)和房利美(Fannie Mac)收到美国联邦住房金融局(FHFA)的指令，将从纽约证券交易所退市。<br />
　　退市之后，&quot;两房&quot;仍然会每个季度向美国证监会(SEC)提交财务报告，股票会在场外柜台市场交易，但它们的股票将不会被大的机构投资者所持有。<br />
　　美国联邦住房金融局给出的退市理由很简单，&quot;两房&quot;股价持续一个月内低于1美元，低于纽约交易所对上市公司股价的最低要求。<br />
　　从5月17日以来，房利美的股价一直低于1美元，而房地美股价一直都处在1美元以上。退市消息出来之后，&quot;两房&quot;股价都暴跌了近40%。<br />
　　市场对&quot;两房&quot;退市反应冷淡，对大盘几乎未产生负面影响。次贷危机前，两房股价从65美元左右的高点坠落，长期维持在1美元左右的价位。所以6月16 日，两房股价再次暴跌也是到了跌无可跌的地步，没啥大惊小怪的了。<br />
　　&quot;两房&quot;股价长期就值几毛钱，为啥没人抄底呢？次贷危机以来，美国联邦政府已经累计为&quot;两房&quot;掏了1450亿美元腰包，要想维持股价在1美元之上，政府并不用再花太多的钱，毕竟现在&quot;两房&quot;总市值还不到10亿美元。<br />
　　显然美国政府已经意识到，即使勉强维持&quot;两房&quot;股价，它们也是将死之人。相对于 &quot;两房&quot;的股票，其债券才是主打，&quot;两房&quot;拥有近5.2万亿美元的债券，&quot;弃车保帅&quot;也是为了减少损失。<br />
　　目前&quot;两房&quot;仍处于亏损状态，须填补的窟窿还将扩大。白宫曾估计，救助&quot;两房&quot;的成本至少为1600亿美元，而美国国家预算办公室(CBO)估计的成本为3890亿美元。但这两个估计都是建立在美国经济正常复苏，房市得以好转的基础上的。<br />
　　从&quot;两房&quot;退市，也可看出美国房屋市场依然低迷。标准普尔 (S&amp;P)/Case-Shiller住房价格指数显示，今年一季度美国房价年率上涨2%，为近几年来的首次年度上涨，但由于税收优惠措施结束以及止赎现象继续增加，第一季度房价仍较2009年第四季度下降3.2%。<br />
　　IMF经济学家Prakash Loungani 根据美国历史上的房价运行周期经验指出，美国房价可能还会继续大幅下跌，跌势持续的时间将更长。在美国经济上升周期中，房价平均上涨39%，而这次房价上涨了113%；在经济下行周期中，房价平均下跌22%，而此次房价只下跌了15%。也就是说次贷危机前的美国房价超涨很多，还远远没跌够。<br />
　　基于悲观的假设，一些机构专家认为，美国救助&quot;两房&quot;的成本可能达到1万亿美元。<br />
　　&quot;两房&quot;的退市根源？<br />
　　今年一季度，美国GDP增长3.0%，复苏势头还不错，居民消费也出人意料地大幅增长。但美国官方仍然对复苏非常警惕，美联储主席伯南克6月9日在给国会作证时指出，&quot;美国经济今明两年仍将继续增长，但不能迅速降低失业率。&quot;伯南克心里明白，如果就业没有本质的好转，居民消费将会是昙花一现。<br />
　　美国5月份失业率为9.7%，非农就业人数增加43.1万人。美国人口普查产生的政府临时招聘人数扩大，使非农就业增加有所虚增。实际上5月份私营部门就业仅增加4.1万人，而4月份有21.8万人。<br />
　　美国每10年会进行一次人口普查，2010年是人口普查年，按照以往规律，5月份都是政府临时招聘进行普查的高点，接下来会逐渐减少。另外，美国失业的深度在加大，目前失业人数中有46%的人失业时间超过半年。我们知道，失业的时间越长，找到工作就越难。<br />
　　现在的美国企业仍小心翼翼，不敢花钱。截至3月底，非金融类企业的现金和其他流动资产，比去年同期上涨了26%。包括工厂和金融投资类企业资产中，有 7%是现金，创下了1963年以来的最高水平。<br />
　　欧洲债务危机使得市场也时刻盯着美国政府的口袋，其财政赤字还在不断扩大。2009年美国财政赤字占GDP的比重高达11.3%，与欧猪五国也差不了多少。<br />
　　奥巴马要花钱的地方不少。<br />
　　刚刚通过的医疗改革法案估计将要花费9000亿美元，BP漏油的补救窟窿也在洗劫美国的国库。虽然奥巴马施压，要求BP承担所有的费用，但关闭近海深海钻井开采可能会加大美国在中东地区的石油争夺。<br />
　　庞大的军费开支随之而来，2010年美国军费预算几乎是世界其他国家的总和。美国2010财年国防预算总额为6861亿美元，其中常规军费预算 5338亿美元，比2009财年增加205亿美元，增幅约4%。今年年底，布什签署的税收减免政策将到期，料定奥巴马还会使该政策的有效性延期。<br />
　　事关&quot;两房&quot;命运的关键是房市，奥巴马曾多次以&quot;房地产市场还很脆弱&quot;为由，回应各界对改革&quot;两房&quot;的呼吁。伯南克在国会作证时明确指出，美国财政状况&quot;长期内不可持续&quot;。所以，在&quot;两房&quot;上的开销能省则省。<br />
　　套牢中国债主？<br />
　　如果美国救助两房的成本是个无底洞，那么中国持有的大量&quot;两房&quot;债券就比较危险了。到底我们持有多少&quot;两房&quot;债，不管是美国还是中国官方都没有透露，毕竟太敏感了。<br />
　　美国前财长保尔森在自传中明确指出，&quot;外国投资者持有超过1万亿美元的由'两房'发行或担保的债券，其中以日本、中国和俄罗斯为主。&quot;根据国外媒体的报道，普遍认为美国国内持有&quot;两房&quot;债券的80%，按照目前&quot;两房&quot;债的规模，外国投资者持有的量应该在1.2万亿美元左右。<br />
　　在次贷危机爆发时，保尔森自传中透露，俄罗斯和中国大幅卖出了美国政府担保的债券(包括&quot;两房&quot;债)。俄罗斯几乎把手中的美国政府担保的债券全部卖光，约为1700亿美元，而中国卖出了500亿。<br />
　　假设当初卖出的都是&quot;两房&quot;债的话，目前日本和中国大致将分摊剩下的近1万亿美元&quot;两房&quot;债，中国估计将持有其中的5000亿美元。标准普尔曾经在报告中指出，中国金融机构持有&quot;两房&quot;债券总额达到3400亿美元。标准普尔的评估数据应该还是比较可靠的，我们实际握有的&quot;两房&quot;债只会比这个多。<br />
　　&quot;两房&quot;退市是否会影响中国持有的&quot;两房&quot;债券的价值？大家普遍还是比较乐观，根本原因是有美国政府做间接担保。5月底结束的第二轮中美战略会谈中，美方也承诺，继续加强对&quot;政府支持企业&quot;的监管，确保其具有足够资本和能力履行其财务责任。<br />
　　股票是股票，债券是债券。但&quot;两房&quot;股票在纽约证券交易所的消失，无疑将大大降低&quot;两房&quot;债券的流动性，中国以后要想再伺机卖出将越来越难，其价值可能将更多停留在账面上。<br />
　　美国政府真的会为&quot;两房&quot;完全买单？不尽然，至少美国已经在思考最坏的情况。6月17日，美国众议院达成一致，将把&quot;两房&quot;列入可能遭到清算的金融实体。这一提案是由克萨斯州共和党议员亨萨林(Jeb Hensarling)提出，还需等待参议院表决。<br />
　　我们不得不佩服美国人未雨绸缪的精神，他们时刻都在思考最坏的结果。<br />
　　一旦经济真的出现二次探底，房价如果继续下跌，两房的窟窿自然越来越大。列入&quot;可清算的金融实体&quot;意味着，如果&quot;两房&quot;未来严重威胁到了美国整体经济，也可以考虑让它们破产掉。这种可能性有多大，谁也不知道，但美国人确实在考虑。一旦走到这一步，中国持有的近5000亿美元的&quot;两房&quot;债也将血本无归。<br />
　　破产，还是太激进。那&quot;两房&quot;未来会沿着什么路径走下去，美国人也在试图回避这个问题。已经在两院通过的金融改革法案，号称是美国自大萧条以来最大的金融变革，却未对&quot;两房&quot;的前途做出任何明晰的处理方案。<br />
　　&quot;两房&quot;退市的危险信号<br />
　　&quot;两房&quot;财务恶化，美国政府力不从心。国会议员以纳税人的名义，向奥巴马政府施压，呼吁将&quot;两房&quot;列入清算名单。&quot;两房&quot;债券不是国债，中国的一厢情愿已露危险信号，政治就是政治，经济就是经济，这是美国人的陷阱。<br />
　　【《证券市场周刊》特约作者罗伯特】美国联邦住房金融局 (FHFA) 当地时间16日发表声明，要求将其监管的美国两大抵押贷款巨头房利美与房地美(下称&quot;两房&quot;)的股票从纽约证券交易所退市，理由是两家公司的股价长期表现不佳。<br />
　　&quot;两房&quot;在退市前总市值10亿美元，可两家公司却亏损了936亿美元，这两家公司还发行了5.2万亿衍生债券。<br />
　　事实上，&quot;两房&quot;退市早有预谋，在奥巴马政府通过的金融监管改革法案中，庞然大物&quot;两房&quot;并没有列入议事日程，相反强调在处理大型金融机构倒闭过程中，要保护纳税人的利益。<br />
　　更为糟糕的是，政府保荐的&quot;两房&quot;在发行债券的时候，一直没有取得政府的担保，美国国会议员则呼吁政府别再用纳税人的钱为贪婪的&quot;两房&quot;债券投资人买单。这对持有大量&quot;两房&quot;债券的中国债主来说，这是一个非常危险的信号。<br />
　　&quot;两房&quot;退市预谋？<br />
　　&quot;两房&quot;退市是纽约证券交易所的规则限制。<br />
　　根据纽约证券交易所规定，若股价连续30个交易日低于1美元，其股票将被摘牌。自 2008 年9月起，在超过30个交易日中，房利美与房地美普通股的价格一直徘徊在1美元附近；最近的30个交易日里，房利美的股票更是跌破了1美元，房地美则在 1.0-1.5 美元之间徘徊。<br />
　　2008年金融危机爆发后，美国政府出面救助&quot;两房&quot;，美国财政部宣称，房利美和房地美能从财政部获得的扶持资金上限均为2000亿美元。美国政府担心2000亿美元救助资金不宽裕，当时还特意强调，这一上限可在必要时加以提高，以弥补这两家公司在未来三年中蒙受的净亏损。<br />
　　到目前为止，美国政府已经拿出1450亿美元救助&quot;两房&quot;，可是效果甚微。&quot;两房&quot;的财务报表显示，截至2009年底， &quot;两房&quot;拥有或担保的住房贷款总价值为5.5万亿美元，其中有500万笔贷款已经处于违约状态。2009年&quot;两房&quot;合计亏损高达 936 亿美元，相当于亏掉了67%的政府救助资金。<br />
　　美国政府早已预料到&quot;两房&quot;的财务在继续恶化，为了不让政府的救助资金打水漂，美国财政部在 2009年12月24日宣布，该部将取消向两大抵押贷款融资巨头房利美和房地美提供扶持资金的上限，为期三年，其目的是缓和市场对有限制性的政府援助措施可能带来之后果的担忧情绪。<br />
　　美国财政部取消了救助资金的上限，表明美国政府对&quot;两房&quot;不抛弃不放弃，可美国当地时间5月25日，也就是希拉里结束在北京举行的&quot;中美战略与经济&quot; 对话的第二天，FHFA在向美国国会提交的年度报告中表示，&quot;两房&quot;未来还将继续亏损，并将进一步向美国纳税人寻求资金救助。<br />
　　FHFA代理局长德马科在国会就&quot;两房&quot;问题作证时表示，&quot;两房&quot;对于美国房市金融系统运作的支持至关重要，但没有进一步财政支持的话，他们将无法行使这一功能。<br />
　　美国国会预算办公室预计，&quot;两房&quot;按照目前的方式运作下去的话，2009-2019年10年间，&quot;两房&quot; 将总共花费纳税人3890亿美元。也就是说，如果美国想完全解决&quot;两房&quot;的问题，除了之前投入的1450亿美元，美国政府还需要投入2500亿美元。美国民调对&quot;两房&quot;的问题相当悲观，认为&quot;两房&quot;的窟窿越堵越大，简直就是两个&quot;无底洞&quot;。<br />
　　美国财政部前法律总顾问彼得・沃利森就站出来抨击： &quot;纳税人正在抵押贷款领域损失大笔的金钱。&quot;沃利森曾就职于美国最大的抵押贷款保险公司MGIC Investment Corp(MGIC)，他对&quot;两房&quot;的救助非常的悲观，&quot;没人知道需要多少资金才能让这两家公司维持其偿付能力。&quot;<br />
　　奥巴马政府一直在推动美国的金融改革，可是美国参众两院通过的金融监管改革法案中，有一个明确的信号，那就是在处理大型金融机构倒闭过程中要保护纳税人利益。国会议员麦凯恩等人更是提议国会放弃对&quot;两房&quot;的继续救助。<br />
　　加上&quot;两房&quot;一点儿都不争气，股价长期低于纽约交易所对股价要求的最低水平，总市值才10亿美元左右。不仅仅&quot;两房&quot;的投资人是开着宝马进去，光着脚板出来，美国政府面临一样的命运。<br />
　　危险的政客信号？<br />
　　&quot;两房&quot;核心资产只有700 多亿美元，但衍生债券总值高达 5.2万亿美元。&quot;两房&quot;退市了，其债券的安全引发了市场的担忧。<br />
　　&quot;两房&quot;是美国政府保荐企业，因此政府保荐成了&quot;两房&quot;的金字招牌，在&quot;两房&quot;的融资杠杆达到70倍的时候，以穆迪、标准普尔为首的全球著名评级机构依然给 &quot;两房&quot;AAA级的评级，他们给出的理由是，&quot;政府对政府保荐企业在治理和监控上越强，其暗含担保和在必要时提供资助的道义上义务就越大，以'两房'与政府的特殊关系，政府一定会给予'两房'特别的支持。&quot;<br />
　　美国国会预算办公室在提交给国会的一份报告中指出：&quot;政府保荐企业证券在联邦法上享受的特殊待遇向投资者发出信号是其发行的证券是绝对安全的。投资者可能会推导出，如果这些证券有风险，政府也就不会给予其不适用联邦保护投资者的有关法律的豁免待遇。&quot;对这样的报告，国会并无明确回应。<br />
　　&quot;两房&quot;债券一直享受着国债待遇，可是政府保荐是不是就是政府担保呢？<br />
　　这个问题一直让美国政府很难给全球的投资者一个明确说法，5月24日在北京召开的&quot;中美战略与经济&quot;对话中，美国国务卿希拉里并没有明确表示，&quot;两房&quot;的债券是否已经由美国政府担保。<br />
　　令人沮丧的是，早在2000 年3月22日，时任美国财政部部长的金赛尔(GenSLer)在国会作证时说，联邦政府对政府保荐企业发行的证券不提供担保。在今年4月3日，美国财政部长盖特纳也明确指出，房利美与房地美发行的债券是否不应该视为主权债券。<br />
　　&quot;两房&quot;退市了，现在有议员提议将&quot;两房&quot;列入清算实体名单，那么中国持有的&quot;两房&quot;债券将何去何从呢？<br />
　　资料显示，自2004年开始，中国对美国债券的持有一直在高速增长，从2004年到2007年惊人地增长三倍，高达到9220亿美元。仅2006年到 2007年间，中国对美国债券的持有增长 66%。<br />
　　2008年年中，美国财政部公布的 &quot;外国持有美国证券&quot;年度报告显示，截至2008年6月30日，外国持有美国长期机构债券总量为1.5万亿美元，其中，中国持有5270亿美元(占 36%)。其中，中国银行、工商银行、建设银行及交通银行共持有价值232.8亿美元的房利美与房贷美发行或提供担保的债券。<br />
　　美国前财政部长斯诺在4月份访问中国，他口口声声强调&quot;美国是一个负责任的国家，我确信购买美国政府债券的人会发现，美国会承担这个责任&quot;。但是面对美国政府如何确保中国持有的&quot;两房&quot;债券安全的问题的时候，斯诺支支吾吾地说，&quot;我不知道。你必须继续等待看看政府的政策是什么。但是，政府没有清空'两房'的债务。我认为美国政府不应该、将来也不会允许购买'两房'债券的人承担损失。&quot;<br />
　　以麦凯恩为首的共和党议员认为，不应以美国纳税人的金钱，补助外资应承担的风险。美国甚至有智囊机构指出，如果中国做了不明智的投资，就应该承担后果，而不是由美国纳税人买单，因为&quot;两房&quot;的债券既然属于&quot;非政府担保债券&quot;，投资者获得了高于国债的利息，就不应该再受益于政府的救市政策。<br />
　　这是一种可怕的信号，美国政客以及民间机构认为，中国政府和其他国家政府就是贪图&quot;两房&quot;债券的收益率比美国政府债券要高，才去购买&quot;两房&quot;债券的，要谋图多一点的回报和利润，就要承受高一点的风险。美国政府没有理由用美国纳税人的钱来补贴中国政府和其他政府投资错误带来的损失。<br />
　　政治是政治，经济是经济，美国政府从来没有明确承认要保全中国持有的&quot;两房&quot; 债券，只是中国人一厢情愿地认为，美国人会利用经济上的压力跟中国政治上进行博弈。相反美国人手握&quot;美元资产安全&quot;这一筹码，在面对贸易摩擦跟人民币汇率问题的时候，可有更大的空间跟中国方面施加压力。<br />
　　房产投资的吊诡？<br />
　　&quot;两房&quot;的停牌让人们清晰地看到，政府在经济发展中，只能是规矩的制定者和公平裁判的角色，而不能扮演全社会&quot;投机性利益&quot;的推手的角色。&quot;两房&quot; 这两个背后站着三姆大叔的机构，是引起2008年全球经济危机的&quot;次贷危机&quot;的推手。<br />
　　房地产泡沫的直接原因，是投机需求的膨胀，使得这种投机需求膨胀得以实现的是金融机构过度放贷。对于全球房地产前些年的价格膨胀，是由于投机需求的膨胀和金融机构过度放贷共同造成的。美国的按揭金融服务，是房地产泡沫和危机的根源。投机需求加上投机性金融服务，房地产的两大要素形成了。美国房地产泡沫的产生与破灭，&quot;两房&quot;难逃其咎。<br />
　　中国金融服务业的不发达，一旦放贷标准放松，尤其是对投资性购房的放贷和利息限制的放松，中国的房地产真正的&quot;泡沫时代&quot;，也就随着来临。从中国 2008年后房产泡沫的急剧膨胀，国有资本通过房地产的&quot;吹泡机&quot;，给中国经济吹起了一个巨大的&quot;非理性繁荣&quot;的大泡泡。中国的国有资本直接介入房地产，有如美国的 &quot;两房&quot;为美国的房地产经济背书。<br />
　　上个世纪，世界上曾有过几次的房地产泡沫。1923年-1926年的美国佛罗里达房地产泡沫，引发了华尔街股市大崩溃，导致了以美国为首的20世纪 30年代的大萧条。20世纪70到90年代，日本出现房地产泡沫。从1991年地价泡沫破灭，日本经济至今没有走出萧条的阴影。 接下来，就是1997至2007年的美国房地产泡沫和次贷危机再一次带起的全球金融和经济危机。<br />
　　从历史的经验中，我们看到&quot;房产投资拉动型经济&quot;的巨大破坏力。 首先，它以人类内心中对财富的过度需求和贪婪作为经济原动力。任何社会的经济发展，都要建立在满足人的合理增长的物质和文化需求的基础上，而不能建立在心理需求的基础上。&quot;投资&quot;需求，就是一种脱离人的现实需要的心理需求。<br />
　　当房地产这种满足人的基本居住需求的经济形式，跟&quot;投资&quot;的心理需求结合起来，就变成了一个经济的魔咒。金融服务以及不断创新的金融发明，为房地产经济的偏离经济发展正常轨道，增添了杠杆的放大效应。 社会的虚假财富，被以倍数加以放大。而房地产的&quot;虚假财富&quot;带来的真实的财富效应，必然带动起一个社会的过度消费和财富的幻觉。<br />
