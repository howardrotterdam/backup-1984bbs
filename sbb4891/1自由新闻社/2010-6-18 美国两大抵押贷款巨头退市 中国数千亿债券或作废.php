													<h2>媒体称中国所持近5000亿美元两房债或将血本无归</h2>
						
						
																											<div id="postmessage_710236" class="t_msgfont">2010年06月28日 证券市场周刊<br />
<br />
伯南克在国会山上的证言，道出了“两房”退市的根源，他们担心经济二次探底。可大量持有“两房”债券的中国人，将来更多时候看到的是账面资产，想套现已经很难了。<br />
<br />
【《证券市场周刊》记者 廖宗魁】6月16日，“两房”宣布退市。<br />
<br />
在美国就业形势不容乐观、财政赤字越来越严重的情况下，奥巴马政府面对脆弱的房地产市场，已经没钱再救“两房”，美国国会议员甚至提出将“两房”列入可清算的金融实体。“两房”想咸鱼翻身的机会已经没有了。<br />
<br />
退市后的“两房”将进入场外交易平台，流动性大大降低。“两房”之前发行的5.2万亿美元债券，其价值进一步缩水，债券投资现正遭遇2008年金融危机之后的二次打击。<br />
<br />
中国金融机构持有“两房”衍生债券将近5000亿美元，“两房”退市后，中国金融机构抛售“两房”债券变得越来越困难。尽管中国政府呼吁美国政府要确保“两房”债券的安全，可美国政府已经力不从心，中国债主已被“两房”债券套牢。<br />
<br />
难以救助的“两房”<br />
<br />
6月16日，美国两大房地产抵押贷款巨头房地美(Freddie Mac)和房利美(Fannie Mac)收到美国联邦住房金融局(FHFA)的指令，将从纽约证券交易所退市。<br />
<br />
退市之后，“两房”仍然会每个季度向美国证监会(SEC)提交财务报告，股票会在场外柜台市场交易，但它们的股票将不会被大的机构投资者所持有。<br />
<br />
美国联邦住房金融局给出的退市理由很简单，“两房”股价持续一个月内低于1美元，低于纽约交易所对上市公司股价的最低要求。<br />
<br />
从5月17日以来，房利美的股价一直低于1美元，而房地美股价一直都处在1美元以上。退市消息出来之后，“两房”股价都暴跌了近40%。<br />
<br />
市场对“两房”退市反应冷淡，对大盘几乎未产生负面影响。次贷危机前，两房股价从65美元左右的高点坠落，长期维持在1美元左右的价位。所以6月16日，两房股价再次暴跌也是到了跌无可跌的地步，没啥大惊小怪的了。<br />
<br />
“两房”股价长期就值几毛钱，为啥没人抄底呢？次贷危机以来，美国联邦政府已经累计为“两房”掏了1450亿美元腰包，要想维持股价在1美元之上，政府并不用再花太多的钱，毕竟现在“两房”总市值还不到10亿美元。<br />
<br />
显然美国政府已经意识到，即使勉强维持“两房”股价，它们也是将死之人。相对于“两房”的股票，其债券才是主打，“两房”拥有近5.2万亿美元的债券，“弃车保帅”也是为了减少损失。<br />
<br />
目前“两房”仍处于亏损状态，须填补的窟窿还将扩大。白宫曾估计，救助“两房”的成本至少为1600亿美元，而美国国家预算办公室(CBO)估计的成本为3890亿美元。但这两个估计都是建立在美国经济正常复苏，房市得以好转的基础上的。<br />
<br />
从“两房”退市，也可看出美国房屋市场依然低迷。标准普尔(S&amp;P)/Case-Shiller住房价格指数显示，今年一季度美国房价年率上涨2%，为近几年来的首次年度上涨，但由于税收优惠措施结束以及止赎现象继续增加，第一季度房价仍较2009年第四季度下降3.2%。<br />
<br />
IMF经济学家Prakash Loungani 根据美国历史上的房价运行周期经验指出，美国房价可能还会继续大幅下跌，跌势持续的时间将更长。在美国经济上升周期中，房价平均上涨39%，而这次房价上涨了113%；在经济下行周期中，房价平均下跌22%，而此次房价只下跌了15%。也就是说次贷危机前的美国房价超涨很多，还远远没跌够。<br />
<br />
基于悲观的假设，一些机构专家认为，美国救助“两房”的成本可能达到1万亿美元。<br />
<br />
“两房”的退市根源<br />
<br />
今年一季度，美国GDP增长3.0%，复苏势头还不错，居民消费也出人意料地大幅增长。但美国官方仍然对复苏非常警惕，美联储主席伯南克6月9日在给国会作证时指出，“美国经济今明两年仍将继续增长，但不能迅速降低失业率。”伯南克心里明白，如果就业没有本质的好转，居民消费将会是昙花一现。<br />
<br />
美国5月份失业率为9.7%，非农就业人数增加43.1万人。美国人口普查产生的政府临时招聘人数扩大，使非农就业增加有所虚增。实际上5月份私营部门就业仅增加4.1万人，而4月份有21.8万人。<br />
<br />
美国每10年会进行一次人口普查，2010年是人口普查年，按照以往规律，5月份都是政府临时招聘进行普查的高点，接下来会逐渐减少。另外，美国失业的深度在加大，目前失业人数中有46%的人失业时间超过半年。我们知道，失业的时间越长，找到工作就越难。<br />
<br />
现在的美国企业仍小心翼翼，不敢花钱。截至3月底，非金融类企业的现金和其他流动资产，比去年同期上涨了26%。包括工厂和金融投资类企业资产中，有7%是现金，创下了1963年以来的最高水平。<br />
<br />
欧洲债务危机使得市场也时刻盯着美国政府的口袋，其财政赤字还在不断扩大。2009年美国财政赤字占GDP的比重高达11.3%，与欧猪五国也差不了多少。<br />
<br />
奥巴马要花钱的地方不少。<br />
<br />
刚刚通过的医疗改革法案估计将要花费9000亿美元，BP漏油的补救窟窿也在洗劫美国的国库。虽然奥巴马施压，要求BP承担所有的费用，但关闭近海深海钻井开采可能会加大美国在中东地区的石油争夺。<br />
<br />
庞大的军费开支随之而来，2010年美国军费预算几乎是世界其他国家的总和。美国2010财年国防预算总额为6861亿美元，其中常规军费预算5338亿美元，比2009财年增加205亿美元，增幅约4%。今年年底，布什签署的税收减免政策将到期，料定奥巴马还会使该政策的有效性延期。<br />
<br />
事关“两房”命运的关键是房市，奥巴马曾多次以“房地产市场还很脆弱”为由，回应各界对改革“两房”的呼吁。伯南克在国会作证时明确指出，美国财政状况“长期内不可持续”。所以，在“两房”上的开销能省则省。<br />
<br />
套牢中国债主<br />
<br />
如果美国救助两房的成本是个无底洞，那么中国持有的大量“两房”债券就比较危险了。到底我们持有多少“两房”债，不管是美国还是中国官方都没有透露，毕竟太敏感了。<br />
<br />
美国前财长保尔森在自传中明确指出，“外国投资者持有超过1万亿美元的由‘两房’发行或担保的债券，其中以日本、中国和俄罗斯为主。”根据国外媒体的报道，普遍认为美国国内持有“两房”债券的80%，按照目前“两房”债的规模，外国投资者持有的量应该在1.2万亿美元左右。<br />
<br />
在次贷危机爆发时，保尔森自传中透露，俄罗斯和中国大幅卖出了美国政府担保的债券(包括“两房”债)。俄罗斯几乎把手中的美国政府担保的债券全部卖光，约为1700亿美元，而中国卖出了500亿。<br />
<br />
假设当初卖出的都是“两房”债的话，目前日本和中国大致将分摊剩下的近1万亿美元“两房”债，中国估计将持有其中的5000亿美元。标准普尔曾经在报告中指出，中国金融机构持有“两房”债券总额达到3400亿美元。标准普尔的评估数据应该还是比较可靠的，我们实际握有的“两房”债只会比这个多。<br />
<br />
“两房”退市是否会影响中国持有的“两房”债券的价值？大家普遍还是比较乐观，根本原因是有美国政府做间接担保。5月底结束的第二轮中美战略会谈中，美方也承诺，继续加强对“政府支持企业”的监管，确保其具有足够资本和能力履行其财务责任。<br />
<br />
股票是股票，债券是债券。但“两房”股票在纽约证券交易所的消失，无疑将大大降低“两房”债券的流动性，中国以后要想再伺机卖出将越来越难，其价值可能将更多停留在账面上。<br />
<br />
美国政府真的会为“两房”完全买单？不尽然，至少美国已经在思考最坏的情况。6月17日，美国众议院达成一致，将把“两房”列入可能遭到清算的金融实体。这一提案是由克萨斯州共和党议员亨萨林(Jeb Hensarling)提出，还需等待参议院表决。<br />
<br />
我们不得不佩服美国人未雨绸缪的精神，他们时刻都在思考最坏的结果。<br />
<br />
一旦经济真的出现二次探底，房价如果继续下跌，两房的窟窿自然越来越大。列入“可清算的金融实体”意味着，如果“两房”未来严重威胁到了美国整体经济，也可以考虑让它们破产掉。这种可能性有多大，谁也不知道，但美国人确实在考虑。一旦走到这一步，中国持有的近5000亿美元的“两房”债也将血本无归。<br />
<br />
破产，还是太激进。那“两房”未来会沿着什么路径走下去，美国人也在试图回避这个问题。已经在两院通过的金融改革法案，号称是美国自大萧条以来最大的金融变革，却未对“两房”的前途做出任何明晰的处理方案。 <br />
<br />
