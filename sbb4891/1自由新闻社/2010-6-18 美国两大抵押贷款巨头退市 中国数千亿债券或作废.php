													<h2>美国两大抵押贷款巨头退市 中国数千亿债券或作废</h2>
						
						
																											<div id="postmessage_690820" class="t_msgfont">当地时间周三（6月16日），美国两大抵押贷款巨头房地美（FreddieMac）和房利美（FannieMae）（以下简称“两房”）表示，已收到美国联邦住房金融局（FHFA）指令，将从纽交所退市。退市后，“两房”股票将在场外柜台市场交易。房地美预计，该公司的普通股以及20个级别的优先股将于7月8日左右退市。房利美则将从纽约证交所和芝加哥证交所退市。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;FHFA命令&nbsp;&nbsp;“两房”从主板退市，表面原因是它们的股价最近一直徘徊在1美元附近，而1美元是纽交所对上市公司股价的最低要求。但有分析指出，作为国有化公司的政策性导向和股东获利的要求之间的矛盾，才是“两房”退市主因。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;这则消息在大洋彼岸的中国也掀起波澜。有消息称，中国方面持有“两房”债券高达数千亿美元。<br />
<br />
退市引来债券之忧<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;“银行已下达封口令，禁止任何人评论”，一家国有大型商业银行内部人士昨日在电话的另一端小声地回答《每日经济新闻》记者。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;2008年次贷危机爆发以来，中国方面始终对所持有的&nbsp;&nbsp;“两房”债券放心不下。美国前财长保尔森在自传《峭壁边缘――拯救世界金融之路》一书中曾提到王岐山副总理不止一次对“两房”债券安全性表达关切。在最近举行的中美战略与经济对话中，美方也承诺，继续加强对“政府支持企业”的监管，确保其具有足够资本和能力履行其财务责任。有专家指出，“政府支持企业”就是指“两房”。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;由于相关数据并不公开，目前市场并不清楚中国方面持有&nbsp;&nbsp;“两房”债券的确切数字。不过，上述保尔森自传中透露，中国在2008年的持有额达数千亿美元。他在书中指出，“外国投资者持有超过1万亿美元的由‘两房’发行或担保的债券，其中以日本、中国和俄罗斯为主。”<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;坊间传闻则称，中国政府持有“两房”债为3760亿美元，商业银行持有250多亿美元。不过，《每日经济新闻》记者向一位专家求证时，该专家不愿透露具体数字，但肯定地说，实际的数额要更多。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;记者查阅各上市商业银行一季报，建行持有外币债券投资组合账面价值83.28亿美元，其中持有美国次级按揭贷款支持债券账面价值1.09亿美元，占外币债券投资组合的1.3%；中国银行2009年末持有约760亿美元的外币债券，但没有披露持有外债的具体种类。记者在中行年报中发现，该行曾持有美国次级住房抵押贷款债券、美国Alt-A住房抵押贷款债券，以及美国Non-Agency住房抵押贷款债券。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;此前，为规避风险，建行为美国次级贷款已累计提取减值准备7.22亿美元，中行为次级住房抵押贷款债券累计计提了12亿美元的减值损失。对于中方是否已经大幅减持“两房”债，《货币战争》作者宋鸿兵在接受《每日经济新闻》记者采访时表示，由于难以找到买家，中方减持的幅度可能不大。<br />
<br />
美方不会坐视不管<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;保尔森曾描述各国官员在次贷危机发生后对“两房”债券可能违约的惊恐之状：“从‘两房’的问题见诸媒体的那一刻起，（美）财政部就不时接到重仓投资于这两家公司的那些国家官员们紧张万分打来的电话。”<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;可以想见，在“两房”退市时，美国财政部也同样会响起来自重仓国家的电话，只不过，此时电话的另一端由保尔森变成了盖特纳。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;作为全美最大的住房抵押贷款融资机构，“两房”的动向始终颇受市场关注。2008年9月，在次贷危机最严重的时候，美国曾向“两房”提供了多达2000亿美元的资金，并提高其信贷额度，而联邦住房金融局也接管了这两家公司。<br />
<br />
　　&nbsp;&nbsp;但“两房”一直没有从危机中走出。美国联邦住房金融局在向美国国会提交的年度报告中表示，“两房”未来还将继续亏损，并将进一步向美国纳税人寻求资金救助。迄今为止，“两房”已合计使用了联邦政府1450亿美元的救助资金，而这个数字还将继续扩大。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;美国联邦住房金融局代理局长德马科&nbsp;&nbsp;（EdwardDemarco）曾在上个月国会作证中表示，“‘两房’对于美国楼市金融系统运作的支持至关重要”。除了中国等海外投资者，6万多亿美元规模的“两房”债也是美国主要银行机构的核心资产，如果任由其价格暴跌，那美国的银行、机构也都会面临破产的危险。<br />
<br />
专家：不必太担忧<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;针对“两房”退市引发的后续问题，宋鸿兵认为，中国不必过分担忧。他认为，退市与“两房”债券价格并没有直接关系，“两房”债的价格由美国政府间接担保，只要投资者相信美国政府，只要美国主权信用等级不被调降，“两房”债券的价格就不会受其股价太大的影响。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;据记者从彭博系统了解，“两房”债券价格在次贷危机一年半以来的整体趋势是恢复的，有些种类的债券价格已经处于历史高位，另外一些种类的价格虽然在2009年出现下探，但在2010年又回升至高点。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;宋鸿兵还表示，跟股票相比，债券要重要得多，美国政府不会让债券出现太大的问题，在万不得已时，美联储会直接购买其债权以保持市场稳定。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;兴业银行蒋舒博士在接受《每日经济新闻》记者采访时表示，债券市场和股票市场的解读方式不同，判断债券的好坏主要看现金流，而股票市场则反映了未来市场的预期。此次“两房”退市主要是美政府回应市场对于“两房”半国有化、半市场化操作的批评，市场不应过度担心其债券价格。<br />
