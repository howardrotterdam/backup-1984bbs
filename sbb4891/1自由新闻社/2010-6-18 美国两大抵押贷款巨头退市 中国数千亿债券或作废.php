<br />
两房退市露危险信号：美未明确承认保全中国债券<br />
<br />
【作者：罗伯特 来源：证券市场周刊】 <br />
<br />
 　　“两房”财务恶化,美国政府力不从心。国会议员以纳税人的名义,向奥巴马政府施压,呼吁将“两房”列入清算名单。“两房”债券不是国债,中国的一厢情愿已露危险信号,政治就是政治,经济就是经济,这是美国人的陷阱。<br />
<br />
　　美国联邦住房金融局 (FHFA)当地时间16日发表声明,要求将其监管的美国两大抵押贷款巨头房利美与房地美（下称“两房”）的股票从纽约证券交易所退市,理由是两家公司的股价长期表现不佳。<br />
<br />
 　　“两房”在退市前总市值10亿美元,可两家公司却亏损了936亿美元,这两家公司还发行了5.2万亿衍生债券。<br />
<br />
　　事实上,“两房”退市早有预谋,在奥巴马政府通过的金融监管改革法案中,庞然大物“两房”并没有列入议事日程,相反强调在处理大型金融机构倒闭过程中,要保护纳税人的利益。<br />
<br />
　　更为糟糕的是,政府保荐的“两房”在发行债券的时候,一直没有取得政府的担保,美国国会议员则呼吁政府别再用纳税人的钱为贪婪的“两房”债券投资人买单。这对持有大量“两房”债券的中国债主来说,这是一个非常危险的信号。<br />
<br />
　　“两房”退市预谋? <br />
<br />
　　“两房”退市是纽约证券交易所的规则限制。 根据纽约证券交易所规定,若股价连续30个交易日低于1美元,其股票将被摘牌。自2008年9月起,在超过30个交易日中,房利美与房地美普通股的价格一直徘徊在1美元附近;最近的30个交易日里,房利美的股票更是跌破了1美元,房地美则在1.0-1.5美元之间徘徊。<br />
<br />
　　2008年金融危机爆发后,美国政府出面救助“两房”,美国财政部宣称,房利美和房地美能从财政部获得的扶持资金上限均为2000亿美元。美国政府担心2000亿美元救助资金不宽裕,当时还特意强调,这一上限可在必要时加以提高,以弥补这两家公司在未来三年中蒙受的净亏损。<br />
<br />
　　到目前为止,美国政府已经拿出1450亿美元救助“两房”,可是效果甚微。“两房”的财务报表显示,截至2009年底,“两房”拥有或担保的住房贷款总价值为5.5万亿美元,其中有500万笔贷款已经处于违约状态。2009年“两房”合计亏损高达936 亿美元,相当于亏掉了67％的政府救助资金。<br />
<br />
　　美国政府早已预料到“两房”的财务在继续恶化,为了不让政府的救助资金打水漂,美国财政部在2009年12月24日宣布,该部将取消向两大抵押贷款融资巨头房利美和房地美提供扶持资金的上限,为期三年,其目的是缓和市场对有限制性的政府援助措施可能带来之后果的担忧情绪。<br />
<br />
　　美国财政部取消了救助资金的上限,表明美国政府对“两房”不抛弃不放弃,可美国当地时间5月25日,也就是希拉里结束在北京举行的“中美战略与经济”对话的第二天,FHFA在向美国国会提交的年度报告中表示,“两房”未来还将继续亏损,并将进一步向美国纳税人寻求资金救助。<br />
<br />
　　FHFA代理局长德马科在国会就“两房”问题作证时表示,“两房”对于美国房市金融系统运作的支持至关重要,但没有进一步财政支持的话,他们将无法行使这一功能。<br />
<br />
　　美国国会预算办公室预计,“两房”按照目前的方式运作下去的话,2009-2019年10年间,“两房”将总共花费纳税人3890亿美元。也就是说,如果美国想完全解决“两房”的问题,除了之前投入的1450亿美元,美国政府还需要投入2500亿美元。美国民调对“两房”的问题相当悲观,认为“两房”的窟窿越堵越大,简直就是两个“无底洞”。<br />
<br />
　　美国财政部前法律总顾问彼得・沃利森就站出来抨击:“纳税人正在抵押贷款领域损失大笔的金钱。”沃利森曾就职于美国最大的抵押贷款保险公司MGIC Investment Corp(MGIC),他对“两房”的救助非常的悲观,“没人知道需要多少资金才能让这两家公司维持其偿付能力。”<br />
<br />
　　奥巴马政府一直在推动美国的金融改革,可是美国参众两院通过的金融监管改革法案中,有一个明确的信号,那就是在处理大型金融机构倒闭过程中要保护纳税人利益。国会议员麦凯恩等人更是提议国会放弃对“两房”的继续救助。<br />
<br />
　　加上“两房”一点儿都不争气,股价长期低于纽约交易所对股价要求的最低水平,总市值才10亿美元左右。不仅仅“两房”的投资人是开着宝马进去,光着脚板出来,美国政府面临一样的命运。<br />
<br />
　　危险的政客信号?<br />
<br />
　　“两房”核心资产只有 700 多亿美元,但衍生债券总值高达 5.2万亿美元。“两房”退市了,其债券的安全引发了市场的担忧。<br />
<br />
　　“两房”是美国政府保荐企业,因此政府保荐成了“两房”的金字招牌,在“两房”的融资杠杆达到70倍的时候,以穆迪、标准普尔为首的全球著名评级机构依然给“两房”AAA级的评级,他们给出的理由是,“政府对政府保荐企业在治理和监控上越强,其暗含担保和在必要时提供资助的道义上义务就越大,以'两房’与政府的特殊关系,政府一定会给予'两房’特别的支持。”<br />
<br />
　　美国国会预算办公室在提交给国会的一份报告中指出:“政府保荐企业证券在联邦法上享受的特殊待遇向投资者发出信号是其发行的证券是绝对安全的。投资者可能会推导出,如果这些证券有风险,政府也就不会给予其不适用联邦保护投资者的有关法律的豁免待遇。”对这样的报告,国会并无明确回应。<br />
<br />
　　“两房”债券一直享受着国债待遇,可是政府保荐是不是就是政府担保呢？<br />
<br />
　　这个问题一直让美国政府很难给全球的投资者一个明确说法,5月24日在北京召开的“中美战略与经济”对话中,美国国务卿希拉里并没有明确表示,“两房”的债券是否已经由美国政府担保。<br />
<br />
　　令人沮丧的是,早在2000年3月22日,时任美国财政部部长的金赛尔（GenSLer）在国会作证时说,联邦政府对政府保荐企业发行的证券不提供担保。在今年4月3日,美国财政部长盖特纳也明确指出,房利美与房地美发行的债券是否不应该视为主权债券。<br />
<br />
　　“两房”退市了,现在有议员提议将“两房”列入清算实体名单,那么中国持有的“两房”债券将何去何从呢？<br />
<br />
　　资料显示,自2004年开始,中国对美国债券的持有一直在高速增长,从2004年到2007年惊人地增长三倍,高达到9220亿美元。仅2006年到2007年间,中国对美国债券的持有增长66%。<br />
<br />
　　2008年年中,美国财政部公布的“外国持有美国证券”年度报告显示,截至2008年6月30日,外国持有美国长期机构债券总量为1.5万亿美元,其中,中国持有5270亿美元（占36%）。其中,中国银行(601988,股吧)、工商银行(601398,股吧)、建设银行(601939,股吧)及交通银行(601328,股吧)共持有价值232.8亿美元的房利美与房贷美发行或提供担保的债券。<br />
<br />
　　美国前财政部长斯诺在4月份访问中国,他口口声声强调“美国是一个负责任的国家,我确信购买美国政府债券的人会发现,美国会承担这个责任”。但是面对美国政府如何确保中国持有的“两房”债券安全的问题的时候,斯诺支支吾吾地说,“我不知道。你必须继续等待看看政府的政策是什么。但是,政府没有清空'两房’的债务。我认为美国政府不应该、将来也不会允许购买'两房’债券的人承担损失。”<br />
<br />
　　以麦凯恩为首的共和党议员认为,不应以美国纳税人的金钱,补助外资应承担的风险。美国甚至有智囊机构指出,如果中国做了不明智的投资,就应该承担后果,而不是由美国纳税人买单,因为“两房”的债券既然属于“非政府担保债券”,投资者获得了高于国债的利息,就不应该再受益于政府的救市政策。<br />
<br />
　　这是一种可怕的信号,美国政客以及民间机构认为,中国政府和其他国家政府就是贪图“两房”债券的收益率比美国政府债券要高,才去购买“两房”债券的,要谋图多一点的回报和利润,就要承受高一点的风险。美国政府没有理由用美国纳税人的钱来补贴中国政府和其他政府投资错误带来的损失。<br />
<br />
　　政治是政治,经济是经济,美国政府从来没有明确承认要保全中国持有的“两房”债券,只是中国人一厢情愿地认为,美国人会利用经济上的压力跟中国政治上进行博弈。相反美国人手握“美元资产安全”这一筹码,在面对贸易摩擦跟人民币汇率问题的时候,可有更大的空间跟中国方面施加压力。<br />
<br />
　　房产投资的吊诡?<br />
<br />
　　“两房”的停牌让人们清晰地看到,政府在经济发展中,只能是规矩的制定者和公平裁判的角色,而不能扮演全社会“投机性利益”的推手的角色。“两房”这两个背后站着三姆大叔的机构,是引起2008年全球经济危机的“次贷危机”的推手。<br />
<br />
　　房地产泡沫的直接原因,是投机需求的膨胀,使得这种投机需求膨胀得以实现的是金融机构过度放贷。对于全球房地产前些年的价格膨胀,是由于投机需求的膨胀和金融机构过度放贷共同造成的。美国的按揭金融服务,是房地产泡沫和危机的根源。投机需求加上投机性金融服务,房地产的两大要素形成了。美国房地产泡沫的产生与破灭,“两房”难逃其咎。<br />
<br />
　　中国金融服务业的不发达,一旦放贷标准放松,尤其是对投资性购房的放贷和利息限制的放松,中国的房地产真正的“泡沫时代”,也就随着来临。从中国2008年后房产泡沫的急剧膨胀,国有资本通过房地产的“吹泡机”,给中国经济吹起了一个巨大的“非理性繁荣”的大泡泡。中国的国有资本直接介入房地产,有如美国的“两房”为美国的房地产经济背书。<br />
<br />
　　上个世纪,世界上曾有过几次的房地产泡沫。1923年―1926年的美国佛罗里达房地产泡沫,引发了华尔街股市大崩溃,导致了以美国为首的20世纪30年代的大萧条。20世纪70到90年代,日本出现房地产泡沫。从1991年地价泡沫破灭,日本经济至今没有走出萧条的阴影。<br />
<br />
　　接下来,就是1997至2007年的美国房地产泡沫和次贷危机再一次带起的全球金融和经济危机。 从历史的经验中,我们看到“房产投资拉动型经济”的巨大破坏力。<br />
<br />
　　首先,它以人类内心中对财富的过度需求和贪婪作为经济原动力。任何社会的经济发展,都要建立在满足人的合理增长的物质和文化需求的基础上,而不能建立在心理需求的基础上。“投资”需求,就是一种脱离人的现实需要的心理需求。<br />
<br />
　　当房地产这种满足人的基本居住需求的经济形式,跟“投资”的心理需求结合起来,就变成了一个经济的魔咒。金融服务以及不断创新的金融发明,为房地产经济的偏离经济发展正常轨道,增添了杠杆的放大效应。<br />
<br />
　　社会的虚假财富,被以倍数加以放大。而房地产的“虚假财富”带来的真实的财富效应,必然带动起一个社会的过度消费和财富的幻觉。</div>

							
							
							
							
