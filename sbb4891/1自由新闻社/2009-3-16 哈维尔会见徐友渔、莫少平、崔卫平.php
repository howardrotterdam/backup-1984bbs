&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; <img src="http://i39.tinypic.com/2hs6a29.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;2009年3月11日下午五时许，捷克前总统瓦茨拉夫-哈维尔在他的办公室，会见了前来布拉格参加“天下一家”（one&nbsp;&nbsp;world）电影节的徐友渔、莫少平、崔卫平。在前一天晚上该电影节的开幕式上，三人代表刘晓波先生和所有08宪章的签署者，从哈维尔手中接过该电影节的Homo&nbsp; &nbsp;Homini（“人与人”）人权奖。<br />
