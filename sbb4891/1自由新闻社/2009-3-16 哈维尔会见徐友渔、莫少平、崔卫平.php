&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp; &nbsp;不同的人对于《零八宪章》的含义和意义的理解会有差异，对于宪章的签署者而言，最基本的共识是：宪章重申了我们关于文明社会的基本价值和目标，其实，这些价值和目标在现存的中华人民共和国宪法以及在中国签字、批准的一系列联合国公约和宣言中也得到了明确的宣示。我们所呼吁和要求的，是要在中国实现它们，而不是与之背道而驰。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;人们注意到了《零八宪章》和《七七宪章》在基本精神方面的一致。是的，我们从上世纪70年代捷克斯洛伐克的《七七宪章》运动和瓦茨拉夫・哈维尔以及其他作家的著作中得到了激励和启示。两个宪章的一致之处来源于两个国家处于相同类型的专断权力和意识形态的统治之下，相似的社会生活氛围和道德状况--不讲真话和不追求正义，也来源于相同的履行国际公约、保护人权的义务和压力。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;和《七七宪章》一样，《零八宪章》在后极权时代提倡一种积极的公民意识、公民道德和公民责任，我们坚持，保障公民的自由、尊严和权利是政府的首要责任，如果政府没有尽到这种责任，甚至一贯地、制度性地侵犯和损害公民的自由、尊严和权利，那么每个人都应该说出自己的真实想法，努力改变现状，而不能甘于生活在恐惧和冷漠之中，满足于自保和自利。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;和《七七宪章》一样，《零八宪章》不是政治反对派的宣言，我们既有批判精神，更有建设性态度，我们关注公民社会在中国的发育，我们的理想和目标是一个健全的社会，我们摈弃传统政治思维中着眼于改变政权和更换掌权者的做法。我们极尽全力争取与政府对话，在这方面，等待和劝戒是必要的，我们既不缺乏勇气，也不缺乏耐心。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;发表《零八宪章》的意图和目的是寻求和解与共识，而不是制造对抗。中国的无权者在争取权利的时候，并不把掌权者的任何失败和挫折都当成自己的胜利，宪章签署者的道德感和责任心远远高于现在的掌权者、过去的革命者。自古以来，中国想掌权的人都把对社会的损害当成对现存政权的削弱和损害，他们以制造动乱、冲突和仇恨来到达自己的目的。签署宪章表明我们与那种做法格格不入，因为损害社会就是损害我们自己。尽管宪章的签署者受到了骚扰、威胁和压制，但我们决不会放弃理性与和平的行为方式。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; <br />
&nbsp; &nbsp; 中国人从100多年前就开始追求宪政民主的目标，但是，由于中国的政治文化传统缺乏自由元素，由于内忧外患不断，由于主要的政治派别和政治力量习惯于以武力而不是协商和妥协解决问题，我们的成就少于挫折、失败与倒退。现在，中国的宪政民主事业面临新的、复杂的局势。斯大林主义没有寿终正寝，它企图利用市场经济来延续生命，二者的结合在新的历史条件下形成一个怪胎，全世界的资本都在为它输血。不少人--既有中国人，也有外国人--把GDP数字等同于政治权力的合法性。如果说，30多年前中国和捷克斯洛伐克一样，压迫的主要形式表现在政治方面，那么，现在中国的无权者既要争取政治权利，又要争取社会公正，争取各种社会和经济权利。《零八宪章》的核心诉求是人权，它涵盖了人之所以为人的各种权利，涵盖了不同地位、职业、民族、性别，具有不同信仰和不同遭遇的人的合法要求。虽然面临各种困难，但我们对于中国的人权和宪政民主仍然充满信心。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;从根本上说，中国的人权事业和宪政民主事业是中国人自己的事，但是，它同时也是当代世界潮流的一部分。就像资本、技术在全球范围内快速流动与扩张一样，信息和新的理念也在跨越国界快速传播。《零八宪章》的签署者和全中国人民、世界各国人民一道关注目前这场全球性经济危机。处于经济和政治双重危机中的我们对来自捷克人民的关注与支持将永远铭记于心，我们也以同等的热情关注你们的进步。<br />
