													<h2>崔卫平 见到了当年“七七宪章”的成员</h2>
						
						
																											<div id="postmessage_60389" class="t_msgfont">听说安排里有与当年“七七宪章”成员的见面，心中便十分喜悦。在《无权者的权力》一文中，哈维尔曾经这样介绍“七七宪章”的成员们：这些人实际上是一些科学家、社会学家、诗人或者工人，拥有他们自己的思想逻辑及他们自己的工作和行为；而在签章上签名，只是做自己觉得应该做的事情而已。他们并非是一些专业的牢骚客。事实上他们往往在成了“持异议者”很长时间之后，才发觉自己已经成了这样的人。即便如此，他们也不是立志要成为这样的人，就算他们将一天二十四个小时全部花费在这项事业上面，这也不是一种职业，而毋宁说是一种存在的态度。这种态度就是要活得真诚磊落，生活在真实中。同样，他们也是一群有六情七欲的人，他们只是说出了其他人不能说和不敢说的话而已。<br />
<br />
这样一种人，他们即使不去签名，也有自己的许多工作要做；同样，即使签完名之后，也并希望能够回到自己的工作和创造的状态，除非情况进一步恶化，使得他们不得不停下手中的工作，以及有人一再干扰他们，不允许他们回到自己的轨道。简言之，他们是一些在人格上和工作上都十分独立的人们，他们有自己的事情好做，对周围环境有自己的独立判断，对于民族的现状与未来拥有道德感和责任感。因此，他们是一些对于权力没有欲望的人们，他们热爱自己的工作和生活。这就从根本上区别于传统上的所谓“职业革命家”，后者不仅在于他们的目标仅仅是为了夺取政权，也在于除了从事日常生活之外的“革命”之外，他们就不知道有什么其他的事情可以做，不习惯也没有能力从事任何别样工作，这种人是最容易对于权力上瘾的。他们对于权力没有免疫力，最终走上了一条与自己的初衷正好相反的道路。<br />
<br />
 这样一种“英雄读本”，与此前接触到的完全不一样，当然内心里也十分认同和赞赏。<br />
<br />
 3月14日下午，电影节再度安排观看中国导演黄文海的纪录片《我们》，那天到场的约有200人，你不能不佩服布拉格观众的热情，这样一部记录当下中国政治语境的影片，即便在中国观众眼里，也会显得有些沉闷，他们居然安静地坐着从头看到尾。当灯光亮起，主持人宣布现在开始讨论，马上有一位美丽的老太太站起来说，对于影片中反复讨论的所有问题，尤其是那些场面，她感到十分亲切，因为这也是她们当年同样经历的。她座位的周围还有一些老人，我凭嗅觉就知道他们来了，尽管电影节事先并没有确切告诉我们“七七宪章”的成员们也会来观看电影。<br />
<br />
影片的放映地点其实是民族剧院，见面安排在其中的一个小剧场，我们三人，他们一共六人。美丽的老太太果然在其中，她的眼角与嘴角弯弯向上翘着，始终像是在微笑。在她旁边坐着一位先生，看上去十分眼熟，心里想着那也许是伊万・克里玛，但是比较以前看过的照片，面前的这位显得秀气多了，后来证实果然是他。1997年试译出版的他的那本随笔集《布拉格精神》，其中描绘不事夸张不失分寸的这个城市的精神，令人神往不已。他曾说布拉格作家由哈谢克与卡夫卡两个传统构成，按此来划分，那么他应该算是更加接近卡夫卡冷隽内向的传统。几年前中国一下子出版了他的五本小说。<br />
<br />
后来美丽老太太给了我一张名片，才得知她与克里玛先生是一对夫妇。其余还有两位同样美丽的老太太，坐在那里头部和腰板挺得笔直，衣着花色典雅而浪漫。另两位先生显得更加苍老凝重，有一位驼背得厉害。我们双双坐在那里，未等开口，便能够感到有一种能量在互相之间传递，那是天然的理解、默契、信任、友善，我甚至感到老人们慈祥中有一种爱怜在内，仿佛眼睁睁看到年轻一辈掉在泥沟里又无能为力。看年龄，他们应该在七十岁左右或者更加年长。<br />
<br />
首先是美丽的克里玛太太问候中国朋友，并说中国是一个大国，这样一个大国，能够向捷克这样的小国学习，这是一件了不起的事情。马上就有坐在她身边的另一位漂亮太太说，在民主和自由的问题上，不分大国小国。然后他们分别向我们提出这样的问题：一、你们这些签名者互相之间认识不认识？二，你们现在的处境如何，签了名之后怎么样？三、我们是因为道德的原因而签名，而你们为什么要签名呢？答曰，就第一批303人来说，并不全都认识，但是其中至少有几十个互相之间是好朋友。至于第二个问题和第三个问题，我们就各人的情况，分别做了回答。<br />
<br />
然后是徐友渔先生发问，他一口气提了两个非常困难的问题：一、争取民主的过程那样漫长，在这个过程当中，有没有成员内部之间互相“内斗”的情况？二、你们在实现自己的目标之后，会不会有失落感？根据许多地方的经验，一些争取民主自由的人，当自由民主实现之后，感到无事可做。徐先生的表达应该比我这里转述的要复杂得多，乃至令我们的翻译娅娜一下子感到语塞。这样的问题，对于这位年轻姑娘来说，不仅是没有思想准备，也许完全没有听说过，我自己也多少觉得有些突兀。此时有人提醒友渔，最好他自己用英文将这两个问题再复述一遍，以便对方回答。友渔应该至少重复了后一个问题。<br />
<br />
因为交流产生的障碍，对方的回答也不是那么响亮。对于“内斗”的问题，答曰，不是完全没有，但是不严重、不重要，因为他们的大方向是一致的。对于“失落”的问题，基本上是否定的，即认为即使民主胜利之后，他们也并不因此而感到自己从此变得无用，这并不构成问题。友渔对这样的答复感到不甚满意，他始终认为对他的问题的翻译是不得要领的，但他也认为这样的问题，是根据中国的情况而提出的。<br />
<br />
我本人倾向于尽管存在交流的问题，但是这样的回答，并不超出我的理解和想象。我曾经讲述过在波兰米奇尼克这样的知识分子，在团结工会内部所做的工作之一，就是防止瓦文萨过度掌握权力，但是捷克的情况不一样。缺少权力欲望是他们共同的特点。第一眼见到哈维尔的直接印象是――这是一个生性弱弱的人，随时准备从不得不扮演的角色当中逃离，又仿佛同时穿着一件紧身衣，将自己深深隐藏在里面，他里面的那个自我迄今完好无损。其实当面的那个问题是这样提出来的：即使您当完总统，但是从您身上仍然看不出来您曾经大权在握。那么，什么是您一生中贯穿始终的东西？您是怎样概括自己的一生的？他的回答是“冒险性”，“冒险性”即“开拓性”，我很能理解这位曾经的剧作家对于因循守旧、成规陋习的反感。<br />
<br />
至于“失落感”的问题，我也可以提供旁证。就是这位伊万・克里玛先生在一次访谈中特地回答了这个问题：89年之后，没有了对立面，他是否感觉写作不是那么来劲？克里玛先生回答道，不是那样的，正是89年之后，他写的比从前快多了，产量高多了。他并没有感到自由带来失重，而认定取消审查制度对于作家的想象力来说，肯定是一件好东西。<br />
<br />
接着我开始向这些前辈们发问，当年他们每一个人签署“七七宪章”时，正在做什么？他们称“每个人的情况不一样。”于是请他们一一介绍。第一位是伊万・克里玛先生，他是一位小说家，签名之后去了美国，一年之后回来丢掉了工作，加入了体力劳动者的行列，第二位是他的美丽太太，当时是记者，签完名之后丢掉饭碗，开始自己做心理咨询，目前这位太太还在做这项工作。第三位说话的太太也是从事心理工作的，第四位是家庭妇女，不知道她的丈夫是谁，气质俱佳，暗花的大披肩令她风度翩翩。第五位、第六位都是神职人员，签完名之后一位失去了工作，另外一位仍然当牧师但是他的孩子因此受连累，而没有能够升上大学。<br />
<br />
我又继续问道，那么你们这些人在89年天鹅绒革命之后，有补偿吗？答曰分两种情况：凡是那些坐了牢的，得到一定的补偿，而没有坐牢而丢掉工作的，则没有补偿。徐友渔这时候接着问道：那些人为什么不去申请补偿呢？众人笑了起来，没有具体回答。<br />
<br />
这时候莫少平律师提出那个转型社会最为尖锐的问题：对于当时那些身居高位而进行政治迫害的人们，有没有对他们绳之以法？回答是基本上没有，只是处理了几个警察。顺便地说，这件事情在1991年哈维尔在与米奇尼克的谈话中是这样解释的：“我们不能打开无法无天的复仇和迫害之门。……关键是找到一个适当的平衡。我们必须采取一种文化的、文明的立场而不是逃避过去。我们必须正视我们的过去，给它们命名，作出结论和实行正义。但是我们必须诚恳地去做，有节制和得当，宽宏大量和富有想象力。如果是面对忏悔和表达悔恨，必须有宽恕的空间。”<br />
<br />
后来我听说了这样一件事情。当年有一个自由欧洲电台（许多在国内不能发表的东西，都在这个电台广播）的播音员，后来发现其实是一名秘密警察，当时给判了四年徒刑，不知为什么没有服刑。但是前两年，此人因为经济的问题，真正被抓进牢里。<br />
<br />
我继而又问，那么，事情过去许多年了，从你们现在的角度，是不是对于当年如此宽宏大量的做法有所遗憾或者后悔呢？马上有人答道：good question!（好问题！）而他们最终的回答则是犹豫的，乃至我现在想不起来他们的准确回答。能够从中体会到的是，他们对于当年的做法，不是没有反省和重新考虑。我曾经见到一位当过七七宪章发言人的牧师，他认为他们当年如此宽松的解决方法不一定很好。<br />
<br />
最后我又问了一个问题，你们最终有多少人跟着哈维尔进了总统府？他们一开始显得不理解这个问题，于是又将问题重复了一遍。答曰，在近两千的签名人士中，只有二十位进了城堡，其余各干各的，比如克里玛先生继续写他的小说。没有任何其他特殊待遇。第二天我在伊万・克里玛先生的家中，克里玛夫人特地向我讲起宪章运动中除了那些知识分子，还有一些自愿担当打字员的女性，这些人籍籍无名，但是同样遭到迫害，对于她们的任何补偿也没有，现在她们年事已高生活在布拉格，有些甚至面临经济困难。对于这些默默无闻的人们，是应该特别要记取的。<br />
<br />
面对这些像邻居伯伯阿姨一样的长辈，觉得非常温暖亲切。在七七宪章之前，他们是普通人，做着自己的事情；签完名乃至遭受迫害之后，他们还是普通人，从来不觉得自己有任何了不起；直至历史最终朝着他们希望的方向实现了，他们更加是普通人，没有参加任何权力的分配，继续他们默默无闻的生活。<br />
<br />
过了两天之后，我们又见到一位当年的宪章人士，他讲了哈维尔当年的一则轶事。秘密警察当然很关心这些宪章人士在搞什么活动。为了破坏这些人士互相之间人与人的联系，警察把这些人家中的电话大多给撤了，但是唯独没有撤掉哈维尔先生。因为哈维尔先生坚持认为自己的一切行动，都是符合当时捷克法律的，都是光明正大的，所以不怕别人知道什么，实际上也没有任何秘密活动。警察留此一人的意思很显然，希望从哈维尔先生家的电话中得知什么他们正在寻找的东西。当然任何人都不希望别人在监视自己的言行，哪怕它们都是光明磊落的。于是大家能够想出的办法是，如果朋友们要聚会谈什么事情，并不事先告诉哈维尔先生，而是去他家直接将他带走。如此说来，丈二和尚摸不着头脑的哈维尔先生，他被朋友带走也十分像被警察带走，因为都是去一个不知道终点的地方。</div>

							
							
							
							
