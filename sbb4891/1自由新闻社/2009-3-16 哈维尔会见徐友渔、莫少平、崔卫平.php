&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;不久前从医院出来的哈维尔看上去气色不错。他表示很高兴见到大家。因为有翻译在场，哈维尔轻松地说我们双方都可以用母语说话，对他来说，讲英语属于迫不得已。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;哈维尔先生向三位赠送了事先签名准备好的他的两本书，新版《给奥尔嘉的信》和《去往总统府以及返回》(To the Castle&nbsp;&nbsp;and Back)&nbsp;&nbsp;。他仔细询问了在座各位的处境、所面临的问题，询问了有关08宪章的一些问题，也讲述了自己当年的一些故事。莫少平律师向哈维尔先生介绍了当前中国法制的有关情况。徐友渔向哈维尔先生进一步解释了有关08宪章中的一些问题。哈维尔耐心地听取了介绍，不时插进自己的看法，并指出民主的道路会是艰难和漫长的。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;崔卫平向哈维尔先生询问了三个问题：<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;第一，什么是他一生中贯穿始终的东西。哈维尔先生答曰：冒险性，即在他没有当总统、当总统期间、以及现在不当总统期间，他都处于一种“冒险”状态之中。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;第二、如何看待“历史”及“历史与个人”的关系，哈维尔强调“偶然性”。就历史的发生和个人道路来说，“偶然性”起着更大作用。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;第三，中国有许多哈维尔的读者，包括一些非常年轻的读者，他们当中的许多人知道哈维尔当年与摇滚乐队的故事，他想向对这些年轻人说些什么？哈维尔先生回答，首先向这些年轻人问好。现在他们可以听到更多自己喜爱的音乐，希望他们能够按照自己的意愿生活。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;崔卫平向哈维尔赠送了《哈维尔文集》。关于这本书，哈维尔讲了一个小故事，他听说，有中国政府的高级官员，私下里在研究这本《哈维尔文集》，众人大笑。三人都拿出自己的那本《哈维尔文集》，请哈维尔先生本人签了字。崔卫平并将自己用来做翻译的那本英文书《Open Letter》，也送给了哈维尔先生。这本书因为长期使用破损不堪，或许它可以保留在哈维尔图书馆。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;会面结束前，哈维尔与每一个人单独合影留念。徐友渔、莫少平、崔卫平三人诚挚祝福哈维尔先生身体健康。<br />
