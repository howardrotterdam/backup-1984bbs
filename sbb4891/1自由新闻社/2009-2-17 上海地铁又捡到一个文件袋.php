																											<div id="postmessage_33775" class="t_msgfont">朱胜文：我的申诉<br />
<br />
作者：朱胜文<br />
<br />
<br />
--------------------------------------------------------------------------------<br />
<br />
【大纪元5月5日讯】编者按：被判刑17年的哈尔滨前常务副市长朱胜文于2003年12月29日在哈尔滨监狱非正常死亡，中国官方新华社解释朱胜文是从狱中三楼的窗户跳下去摔死的。朱胜文的死亡引起人们许多的猜测和传闻。早在1997年12月22日，朱胜文在狱中写了《我的申诉》,寄望国际上的人权组织给与帮助未果。 现转载大参考《我的申诉》全文。<br />
＊ ＊ ＊ ＊ ＊ ＊<br />
<br />
　　哈尔滨市中级人民法院：<br />
<br />
　　我原是哈尔滨市政府常务副市长，1997年12月12日市中级法院以受贿和财b来源不明罪对我开庭审判，在法庭上我就两个重大问题做了陈述，根据合议庭的要求写成的材料，请阅审。<br />
<br />
　　　一、我和岳玉泉有重大利害关系，岳利用职权对我进报复陷害我，请求岳玉泉回避我的案件<br />
　　我被捕以前与岳玉泉同哈尔滨市政府常务副市长，关系一直比较疏远，1996年初，我连续处理了两起与他有直接关系的违法事件，触犯了他的隐私，使他怀恨在心。<br />
<br />
　　第一件事，我处理了他挪用2,000万元市社会劳动保险基金的问题<br />
<br />
　　1995年末，市委根据工作需要，将市长分工做了调整，一直由岳玉泉分管的市劳动局划归我分管，此后不久，大约96年2、3月份，一天市劳动局长梁三荃带著另一名同志（是副局长还是处长，我刚接手不太熟悉）到我办公室，说有件急事要汇报。梁三荃说，最近省总工会、审计局、省监察厅联合调查全省社会劳动保险基金使用情况，在调查中发现我市劳保金有2,000万被挪用，借给了民营企业聚兴公司搞房地b，省要求哈市立即将谁决定此事及决定过程报上去。我问是谁决定的，梁三荃说，是岳市长，说著拿出岳的批示，我当时十分震惊。在我分管市政府资金六、七年间，第一次遇到这样严重违法违纪挪用专项资金的情况，我说你们胆子怎麽这麽大呢？你们是管这笔钱的都明白，这是全市退休职工的救命钱，像救灾款一样专款专用，严禁挪用，更何况你们借给民营企业搞房地b，这更违法，这笔钱一旦出了问题谁能负起责任？梁三荃解释说，这事我们哪敢定，市长怎麽说我们怎麽办，现在请您定一下，岳市长的批示交不交上去。我问岳市长原来有过什麽意见没有？梁说岳市长不让往外露，所以我们从来没拿出来过，现在你分管我们得请示你，交不交。我当时感到情况复杂，很难处理，主要考虑我历来与岳关系不睦。刚接手，就把他的批条捅出去，岳肯定会对我有想法，而且岳有过明确意见，便答覆说，既然岳市长有过明确意见，就按岳市长意见办暂不交上去，省里问就说我刚接手情况不太清楚，等搞清楚再向他们汇报。接著就对梁三荃讲，现在当务之急是必须立即将这笔钱要回来，过去的问题先不说。如果资金要不回来，那后果不堪设想，梁三荃说：朱市长我们都快急死了，天天去要，马玉福（聚兴公司老板）原来说用几个月，结果现在都有两年了，一分钱也没有还，他盖了一大堆房子在那里都卖不出去。藉口一屁股债，连银行利息都还不了，政府的钱，他根本排不上。2,000万元不是一个小数，我看很危险，我们挺害怕。我当时又强调一定要派人尽快要回，保证政府这笔资金不能损失一分钱。我说，过去的事怎麽定的，我不想多问，今后出现类似情况追究你们的责任，梁三荃笑著说，我们是给市长管钱包的，你怎麽说我们怎麽办。我让他们把追缴情况及时向我报告，此事处理完后，岳对我态度明显冷淡，我们的矛盾由此开始激化。<br />
<br />
　　1996年5、6月市评选劳动模范，我是评选领导小组组长，岳玉泉是副组长，在我主持领导小组会议时，市总工会副主席韩玉琴介绍候选人时，谈到聚兴公司老板马玉福，说工会征求意见时普遍反映该公司贷款太多，资不抵债，有争议。因我听过梁三荃的汇报（组会上说），便表达意见，主张慎重一些好，建议让大家讨论一下。讨论时很多人同意这个看法，主张慎重好，但由于岳玉泉一再坚持，最后还是评上了，这使得岳和我的矛盾又进一步加深。 　　以后我又问过几次这笔钱的追索情况，直到我被捕时，尚未归还一分钱。<br />
<br />
　　其实我当时处理此事，完全是从工作出发，而且还维护了岳的面子，但是人无害虎心，虎有伤人意。我没有过问过，他批钱挪用的情况，也尚未向任何人说过此事，只是因怕出现更严重的问题而坚持原则，进行清缴。岳担心我知道了他的隐私，对他构成威胁，对我怀恨在心，我们之间的矛盾进一步激化。<br />
<br />
　　第二件事，我处理了他指使市政府工作人员，制造非法文件，金街办非法免税2,000万元的事<br />
<br />
　　　　时间也是在我听梁三荃汇报的前后，大约96年3、4月份，根据95年省纪检委调查国贸城免税的情况结论，了进一步加强税收管理，市政府决定，全面清缴欠税，特别是地下人防工程企业一律按章纳税（在1996年前市政府扶持人防企业，对全市人防企业都暂不征税）。<br />
<br />
　　在市国税局清税时，发现金街欠税累计超过一千万元，当年欠税三百多万。当税务人员前往收税时，金街总经理黄桂英态度蛮横拒不交税。国税局将此情况向我做了汇报，请示怎麽办，我说清理欠税是全市性的，哪个企业也不能例外。特别是人防企业，中央调查时已有了明确意见，一律按章纳税，必须认真执行。税务局人员又去征税，又被顶回。过了两天，我到税务局听取全市清税工作汇报时，赵局长向我反映清税工作时，反映金街总经理黄桂英昨天拿著一个市政府关于给金街免税五年的协调会议要去找他，告诉说金街的税市政府已经全给免了，以后税务局不要再去催税了。赵问我知不知道此事。我当时听后很吃惊，又半信半疑说这绝对不可能，我又问一下协助我工作的谢万荣秘书长，是否经办过此事，他说他不知道。于是，我立即让我的工作处于处长去办公厅将此件调来让我看一下。奇怪的是于去调时，秘书处工作人员说岳市长告诉这个文件谁调也不给，因岳市长是常务副市长他们不敢给。我当时十分气愤让于处长再去一次，并告诉秘书处，我也是常务副市长，又分管税务和办公厅，我有权调出此件。秘书处无奈，将此件拿出来，我一看纪要是新上任的市政府副秘书长吕尊法协调的，协调的内容是将金街免税四年（从1993到1998年），大约2,000万元左右（这个数位是税务局跟我讲的，纪要上没有的）。纪要和国贸城的是一模一样，文件抄送栏，既没有给我，也没有给汪市长。因此事重大，我立即向汪市长做了汇报，汪市长看完文件十分气愤，连说简直是胡闹，立即撤消。并让秘书马上打电话找吕尊法（当时吕在太平区参加一个会议），在电话中汪市长严厉批评了吕，并连声说你这是犯罪！让吕立即赶到他办公室来，十分钟后吕赶到汪市长办公室，汪说谁让你协调的，吕答岳市长，并说李市长也知道。汪问你有这个权力吗？你知不知道是什麽问题，你这是犯罪！吕说我刚到政府不知道怎麽回事，市长叫我怎麽办，我就怎麽办。我当时很气愤对吕讲，岳市长对国贸城免税调查非常清楚，他怎麽能明知也故犯呢？我又问吕，涉及到免税问题什麽不抄给我也不抄给汪市长，调阅也不让，搞成个秘密文件，吕没回答。汪市长当即决定由我负责立即以市政府名义起草一个文件将此件撤消。<br />
<br />
　　以后金街总经理三次到我办公室，让我暂时不要发撤消文件，说她去国家人防办跑一下争取免税。我对黄说那是你企业的事，你能跑下来，我是负责全市经济工作的，对企业对地方经济有好处的事我都支援。企业有困难可以向政府汇报，比如税可以缓但必须按合法程式，不能搞违法的事，（结果她跑了一圈，也没跑下来，我给她扣了三个月的税）国贸城的情况她不是不知道。<br />
<br />
　　在以后几次谈话中，黄多次来劝告，并威胁地对我说，朱市长，我听大家都说你是个好人，我也很佩服你，我跟你说一句真心话，听不听由你，你这麽办，将来对你非常不好。最后这一句话她重覆多次，我问你什麽意思，她也不回答，只是重复这句话。我当时并未在意，只是告诉她，这是汪市长定的，我必须执行。事后我想起，在此前几天市国税局长赵晓明有一天找我谈话，说他压力很大，我问什麽事，他说，岳市长把他叫到他办公室跟他说话很不客气，问他怎麽同金街过不去，赵向岳汇报了金街的实际情况，岳在谈话中说：我管公、检法，哈尔滨抓谁不就是一句话吗？赵说我没犯罪你也不能抓我，岳很生气。我当时听后虽然很震惊，但没发表意见，只是安慰赵晓明局长说，这是咱们的本职工作，该坚持就得坚持不要怕。后来事情发展真的按他们的说法实现了。<br />
<br />
　　这件事后，我和岳玉泉的矛盾达到了顶点。我们俩除会议外，互相不说一句话。岳的报复心极强，哈市中无人不晓。因我坚持原则，处理了这两件与他直接有关的违法事件触动了岳的敏感神经，他认我与他做对，怀恨在心，所以国贸城案件之机，凭藉他任专案组副组长的职权，用非法手段制造假案对我进行陷害。（具体我在第二个问题中说明）<br />
<br />
　　鉴于我和岳玉泉有重大利害关系，他现在任专案组副组长直接领导指挥对我的案件侦察和审判是十分不合适的，了保证我的合法权益，维护法律的公平和尊严，根据中华人民共和国刑事诉讼法第三章第28第第二款，我请求岳玉泉回避我的案件。<br />
<br />
　　二、国贸城总经理张庭蒲向我行贿七万元一案，是严重的逼供信造成的假案冤案<br />
<br />
　　（一）国贸城案件的起因和背景<br />
<br />
　　对国贸城企业及其免税问题，1995年之前，我并未做过认真的了解，直到1995年省纪检委调查时，因涉及到我，我才找外贸局的同志询问了情况，对事情的全貌有了一个基本的了解。<br />
<br />
　　国贸城是外方国源公司和中方哈人防办合办的中外合作企业。创办时中外方原意办成一中外合资企业。但筹办的过程中，中方（市人防办）的上级考虑人防办工程是国防工程，根据有关法律规定，中外合资企业解体后，要按投资比例划分固定资b，这样如果办成中外合资企业意味著若干年后，企业解体时将有一部份国防工程划规外方所有，从国防角度看是不合适的，因此要求办成中外合作企业（按规定中外合作企业解体时，外方不分固定资b）在当时中外合资和中外合作企业享受同等减免税的优惠，因此外方也同意了。但企业成立后，国家政策作了调整，中外合作企业不再享受减免税优惠，外方感到吃亏了，要求中方调整，但中方因某种原因无法办一直拖下来，后来在一次三资企业协调会上，该企业向市政府提出此问题，要求给予解决，否则外方将考虑撤资。会上根据惯例，我请外资局杨局长了解一下情况，帮助研究一下。过了一段时间，杨向我汇报说他和税务局研究了该企业情况，税务局认，考虑该企业组建时的特殊情况和组建后对哈市就业等方面的贡献，吸引外资从“三个有利于”出发，同意比照中外合资继续给予减免税优惠，并且由税务局形成了一个纪要。我当时提出，既然税务局已经同意了，那就按税务局的意思办把。过了一段时间杨又来说，税务局意思最好由政府正式协调一下，我表示涉税问题，还是由税务局自已去定，必要时可以向上面请示一下。后来杨和税务局如何研究的及谢万霞如何协调的我没过问，也不太清楚。此后谢万霞形成一个纪要给我，因我的印象中税务局已经同意，加之当时市政府扶持全市人防工程综合利用，对全市人防地下企业（包括金街、国贸城等）全部未征税，所以也并未在意，只当做一般专阅文件，在上面签了个“朱”字，未表任何意见，此后也未过问过此事。<br />
<br />
　　1995年省纪检委调查此事时，我已如实向调查组人员介绍了解到的情况，而且表示上级如追究此事但时，由我来承担领导责任。根据调查组意见对国贸城恢复征税，以前欠缴的同时清缴，根本没有给国家造成什麽损失，事情的经过就是如此。<br />
<br />
　　（二）张庭蒲向我行贿七万元，完全是逼供信造成的假案冤案<br />
<br />
　　1996年10月24日我刚上班，被汪市长叫到他的办公室，汪刚出国回来，原打算开一个市长秘书长碰头会，汪市长简单问了一下情况后，说监察厅有人找你，我问什麽事，汪说是国贸城的事，我说国贸城我有会麽事呀？这时监察厅同志让我上他们的车，将我带到厅大楼。两位专案组的人员王林、梁大树已在那里等候，到监察厅后，他们要我家的地址。我意识到要搜查我家，我说我是省人大代表，你们拘禁我，搜我的家有手续吗？他们说，我们是省委专案组，省市人大都在省委领导下。当天晚上，汪、梁两人将我带到专案组大楼，锁到铁椅子上。<br />
<br />
　　当天夜里，由王林、梁大树等四人对我开始侦讯。开始便问国贸城问题，我对他们说我和国贸城没有经济往来，如果有一分钱问题天打五雷轰，我相信组织能够调查清楚。王林说，你是案子里露头的，你必须交待。因我心里清楚我在国贸城没问题，什麽把我抓起来。我意识到肯定有人做手脚陷害我，而且肯定是岳玉泉。因他抓国贸城案件。由于我态度坚决，僵持了好长时间无结果他后便停止了讯问。都出去了，过了一会又都进来了，说国贸城问题暂时不说了，你继续考虑，现在谈你哈市医药股票问题。我当时完全以一名党员对党的忠诚老实态度，如实讲了我家买过二万三千元股票（其中我妻子买1.3万元，张春晖代我买一万元），他们问的很详细，不断取笔录。当我讲出张春晖给我买一万元股票卖后送我十万元时他们态度开始大变，一方面说我态度好，一方面侦讯更加严厉，但在此期间没有任何打骂行，这样大体过了两天，一会儿笔录，一会儿审，一会让我自己写，在这两天中我没有回房间，不时传来拷打和嚎叫声，我心理十分害怕和紧张，但当时觉得他们的态度好，我毕意是个副市长，他们不一定能打我吧？<br />
<br />
　　大约是26日半夜，房九林著卷看郑宗进来了，王林，宋铁耕，梁大树，孙德全，等四人紧随而进，个个态度严肃地站在桌子旁，我面对房坐在屋角的铁椅子上，气氛十分紧张，由房主审（这是房亲自主审我的唯一一次），房把卷宗放在桌子上说，你不是老说国贸城没问题吗？今天就审国贸城问题。<br />
<br />
　　房说，我就是个小科长，没审过这麽大干部，但告诉你，能把你请进来，也肯定能把你拿下。然后拿起卷宗翻给我看，说你的证据有的是，不能随便抓你，你是市长啊。然后开始问我什麽时候认识张庭蒲的，我说和张庭蒲吃过一次饭的详细情况从头到尾说了一遍。然后问还有接触没有，我说只有这一次，方又问真没有，我说真没有。然后其余几个人插话说，告诉你就你哈医药这十万元就可以判死刑，你老实交待了争取个好态度。我说如果有，哈医药我能说什麽国贸城我不能说？我确没有。这时方满脸怒露容，一拍桌子问到底有没有，我说没有。房夹著卷宗就走出去了。<br />
<br />
　　接著王林、宋铁耕等四人便走上来拳打脚掰胳膊往上掀，因我被锁在铁椅子上无法躲，只好大声喊叫。大约打了20分钟，房九林进来了，他们四人也住手了，我大汗淋漓，喘粗气，直觉得血往上涌头昏眼花，这时房让他们把我从铁椅子上放出来，房站在地中问“有没有”，我还说没有，王林说，告诉你你是案子里露头的，你说也得说不说也得说，你编也得编出来，我听到这句话极震惊。我完全明白，确有人故意陷害我。王林又说：“别说你这一点事，杀人犯杀了人，明知死罪也得招”。接著问我能不能说，我还是说真没有。没等我说完，王林等四人一起上来，开始踢打，这次房没出去始终站在旁边，但房没动手。接著让我跪在地上两人按著肩膀住上掰胳膊，两人在旁边踢肋，腰和背，我大喊我有心脏病，他们边打边说：有病当个????毛。这次打得我浑身疼痛，喘气困难。他们说你能挺，咱们就照样来。我预感到今天晚上不拿下我的口供，他们不会罢休。四个人停下来，房让我站起来，说能不能说，我只得说能说，然后他站在桌子后面，其余四个人站在我周围摆出随时打我的架式。房问张庭蒲去过你办公室没有？我说去过（实际根本没去过）。去前给你打电话没有，我说打过。又问当时拿没拿什麽东西，我回答不出来。房又问，拎没拎过包，我只得按他的话往下编，说拎过。里面装什麽东西？我自言自语的说，可能是照相机吧（因只能瞎猜），他们说不对，我说那是摄相机，他们说也不对，说了几个都不对，他们半嘲讽的说：“有那个东西，这些都可以买”。我反问一句，“那是钱？”房他们说，“这回说实话了”。房又问，你打开包在里面装多少钱，我说“一万”时，四个人一齐上来掰我胳膊说不对，我说“二万”，“不对”。越掰越高我疼痛难忍地说“三万”，他们说不对，我又说四万，直到我说五万，他们都住了手说，“对了，到底说实话了”。<br />
<br />
　　接著问，他给你钱了，你怎麽给他办的免税，我就把95年当时调查时了解到的情况，按他们审问的要求编了12月初如何布置的（实际上当时我在北京开经济工作会议）。<br />
<br />
　　接著又问，你给他们办免税，他们怎麽感谢你呀？我意识到可能还有一次要审我。我说他没感谢。房说，你给他办完事，他能不表示吗？你又不老实。然后说，你想想春节张庭蒲去没去你办公室表示感谢。我想既然编了一次，只好继续编下去了，又编了春节后第一、二天张去给我送钱的情节（实际上当时我在上海和海邦集团谈判），问第二次给你送多少钱时，我说：“一万”，他们说“不对”。我想可能和第一次样一样他们想我说“五万”，但说了后他们却说办公室已经给你五万了还能给那麽多？我于是又忙乱说是“二万”，这回他们说“对了”。<br />
<br />
　　然后又问我钱放哪去了，我开始说，让我出差买东西了。他们问，你出差买东西花那麽多钱，我回答不出来，就改口说拿回家交给我爱人了，了不让我爱人受牵连，我说我分六、七次第每次给6,000元，三天一次分批拿回家的（在北安监狱第二次承认时，我又说放到袜子去了）这就是我第一次承认国贸城受贿的经过。<br />
<br />
　　我招供后，他们连续分四、五组，由不同的人，反覆写笔录（这些人有参加侦讯的，也有没参加的）。我在招供时按他们要的内容一遍又一遍的重覆，大约有五、六次。有时说的不对，他们就拿招供时笔录纠正，我又再编，让我签字时，笔录的日期、审讯人一栏内容大都空白，我只写一个名字，当时我不明白怎麽回事，也未在意。<br />
<br />
　　这是专案组第一次对我进行的严重刑讯逼供，在那天第二天，我就对专案组的王林，梁大树、孙德全等说，国贸城我一分钱也没有拿，有人整我，他们说：你们领导之间的事我们不管，我们就搞问题，你说没有没用，笔录口供都取了。<br />
<br />
　　专案组第二次刑讯逼供是95年11月初，大约在被关押后第10天左右。<br />
<br />
　　国贸城口供之后，他们又让我交待玛克威，北方花园和黄通国（国贸城外方老板）向我行贿的事。特别是玛克威公司，我跟他们说绝对没有，他们说肯定有，你给他免那麽多税（谢万霖曾给该公司开会协调免税，我确实不清楚此事），他能不感谢你吗？告诉你，不但有，而且不是小数，不是几万块钱，我说那还是几十万？他们说那你肯定清楚。我预感到他们还想用国贸城的办法逼我出假口供，然后罗织罪名，置我于死地。于是我反覆向他们讲，玛克威公司大门朝哪开我都不知道，北方花园是开工典礼时，发过纪念品；黄通国人很吝啬，我去香港，他请我们吃饭时用喝剩过的洋酒招待，他怎麽会给我钱？他们说到时候你就有了。<br />
<br />
　　果不出我所料，11月5、6号的半夜，王林，宋铁耕，梁大树，孙德全四人突然气势汹汹的进来，手里拿著警绳，电棍。宋铁耕大声说，省委领导批示要严肃处理你，今天晚上你必须交待。我说我真没有，然后把前几天跟他们说的话又重覆一遍。王林说，我告诉过你多次，案子里露头的必须讲，我看你也别说假话了，然后叫我脱下西装上衣，穿上他拿来的一件秋衣（可能怕打出严重外伤），我知道要下狠手了。接著把我按扒在桌子上，将我双臂用警绳反绑在后背，绑完后，对我说，你不是下过乡，吃过苦吗？今天你要能挺过两绳算你好汉，上过两绳后，把你放到街上，你要能跑出五十米算你好样的，然后问，你交不交待，我说我真没有，求求你们，我身体有病，真的抗不住啊！他们说，知道抗不住就说，见我不吱声了，他们都站起来，围住我，你一会就说了，接著开始往后掀胳膊，因我被按在桌子上，无法动弹，掰得疼痛钻心，满头大汗，只能大声喊叫，他们拿一块抹布堵上我的嘴，一边堵一边骂：“喊你妈个x”。宋铁耕用警棍猛抽我的上臂，打到后来我的两臂已经没有感觉了，他们又猛打几下，我毫无反应，便互相说，先给他松开活活血，别整残废了，然后把警绳给我松开。让我两手高举，往上扶墙站著，当时我的两臂已经E不起来了，他们扶著我的胳膊搭到墙上，让我站了十来分钟。<br />
<br />
　　接著还问我交不交待，我说没有，他们又第二次让我趴在桌子上，接下来用第一次的方法重新把绳捆上，还是两胳膊掰后，这次他们让我站在地中间，四个人轮番用电警棍抽拳打脚踢，宋铁耕掀开我的衣服，用电棍触我的肚皮、腰和背部，每一触，一阵刺痛疼得我乱滚乱躲。他们把我按到床上，往后背触电棍，我往地下滚，大喊叫，他们用抹布堵上我的嘴，继续打了一会、停下来，让我坐到椅子上，我这时浑身一点力气也没有了，只是大口喘气，没想到他们突然从床上抓起一被子蒙在我的头上，我憋得眼冒金花，然后一片黑从椅子上栽到地下，他们把被子拿下去，又把我扶到椅子上，骂我学会“放秋”（就是装死、耍赖的意思），他们在我实在挺不住了，就把绳子松开来，这时我已绝望，只求一死。<br />
<br />
　　我坐了几分钟，他们问用不用第三次。我说你们饶了我吧，我受不了。我已感觉抵抗只能换来更大折磨，但要招供，又不知道他们让我承认什麽，只听他们口气，是几十万元，我要真签字画押了，一旦死了，他们可以说我畏罪自杀，况且，我连玛克威大楼都没去过，编都有没法编，于是我说北方花园吧（因我和该公司曾经有几次接触，还能编出来）。他们说要交待就交待大的，别整一万、二万的，我们又不是叫化子，没见过钱，我于是从三万编起编了三次，凑七、八万块钱。我看他们听完后也半信半疑，但是说有成果，因我已经成了一团烂泥就让我脱下秋衣，结束了审讯，我当时真有死里逃生之感，欲哭无泪，像个傻子的呆坐在那里，半天听不清他们在说什麽。<br />
<br />
　　他们接下来，就连夜取笔录，这是我在专案组碰到的第二次也是最强的刑讯逼供，因口供是瞎编的，根本对不上，他们以后也没有提这两件事。<br />
<br />
　　此后，一连几天，浑身伤痛难忍，喘气肋部一动就疼，浑身清一块，紫一块，摸哪哪疼，夜晚彻夜难眠。一个多星期后，因浑身多日不洗已发臭，他们让我擦身子，我脱下衣服发现两肩两臂都是大片的紫黑色，专案组在场的人都看见了。<br />
<br />
　　从此以后，专案组可能真正感到整不了什麽大的东西，于是转问我接近的领导、干部、朋友、外商、大企业负责人，根据线索（大多是岳玉泉搞的）逐个列单进行逼供，此后逼供没有警棍、警绳等械具，多是拳打脚踢和掰胳膊，但那时我的身上到处青肿，碰一下都钻心痛，身体也虚得毫无力气，经不起半点折磨。在逼供情况下，出了很多假口供，有的只是有其人，但其事严重夸大（所供情节，大多是有其人无其事），还有一部份根本无其人，也无其事。以至后来有的线索（如呼兰开发区主任给我送20万元）连专案组也不相信了。<br />
<br />
　　我在专案组碰到的第三次严重的逼供是要我承认国贸商城总经理黄福林（外方老板黄通国侄子）行贿五万元，时问是专案组重新分工前的夜里即12月4日，因第二天，我将由王林等四人看管。变成梁大树、祁浩宇二人看管、审讯。那天晚上，因重新分工了，晚上人进进出出，互相唠叨，我锁在铁床边上独坐。大约到晚上十点多锺，突然王林和张生走了进来（张生不负责我的专案，从未参加过审讯，但我听他们唠叨说此人打人挺狠），他们俩进来，我不知是何意，王林对我说，明天我离开你了，你是不是挺高兴。我没吱声（我心里确实高兴，因他打我打的特狠）。王说，今天晚上还有最后一个问题，叫你交待，我一听明白了，还要逼点什麽东西。可能让张生有个立功机会（因他们是同学）不然明天分开，他们就无权提审我了。但我不知道他们要逼什麽。王林和张生这次很直截了当说我俩，也甭跟你废话，就来真格的，今天不行就给你吊起来，照一宿来，你捉摸能不能挺过来。张生这时按我的脖子上来猛踢几脚照肋部狠打，问：“用不用多废话”？我说我不知道，让我交待什麽问题。王接著问你认识黄福林吧，国贸城副总经理，外方老板黄通国的侄子。我说在人防办，宴请黄通国时，见过他一面，给过名片，王说：黄通国通过他给过你钱没有，我说没有，他们俩立即很恶煞地冲上来，揪住我膀领，用拳猛打，边打边说：看来你不见棺材不落泪啊。这时王林往外走说去拿绳子去，我看他们真要吊我，想起玛克威那天情况，我心发抖，我说：我求求你们俩，你看我身体这个样子，真经不住了。王站住了对我说，你别装那个熊样，你说不说吧，不多就五万块钱。我看他们是不会放过我的，既然假口供多了，我也并不在意了，当时想法只有结案时，再一块说吧，假的真不了。于是我说你们让我说啥，你写吧，我签字。王林说，你少来这一套，是你交待还是我交待，然后拿出一份材料问，黄福林去过办公室吗？我说去过（突际上根本没去过）又问怎麽去的，我就按张庭蒲模式一模一样地编了一遍。又问给没给钱，我说给了。给了多少？我说五万（因数目开始他们就告诉我了）。又问什麽时间，我说大约94年3、4月份（因那时我到俄罗斯出访）。又问因什麽给的，我说因要免税，他们说那时还没有免税的事呢，我回答不出来，王说你想想黄通国是不是不时给你打过电话，让你关照一下他们的审计问题，我说是，又按他们的要求一句一句地编，然后又问钱哪去了，我说一部分出差买东西了，一部分拿回家了。<br />
<br />
　　由于这次他们让我说啥我说啥，模式又按张庭蒲那次编，所以免遭了一顿折磨，但充分证明了他们有关我国贸城材料的虚假性，因口供实在驴头不对马嘴，所以95年4、5月份由专案组李伟、龚长久又取了否定笔录。但奇怪的是，我记得十分清楚，他们逼我的时间是12月4日，而他们给我看的笔录时间是11月8日，我当时不知道怎麽回事，因是否定笔录，所以我也没跟他们认真，只说那可能是我记错了。在法庭上，他们拿出所谓25二BGB0005f我张庭笔录时，我才明白，他们在录笔录时什麽将日期、提审人、地点栏目带空白，什麽一个事，多次不同人反覆取笔录的原因，是他们可以根据需要，随心所欲填写，以便日后我翻供时拿出来，证明我说的是假话，手段卑劣到了极点，这正证明他们心虚。<br />
<br />
　　从1996年12月5日到97年1月5日我由519房间转到518房间，饮食和睡眠条件有改善，提审也减少了，基本上没有打骂、体罚等情况，但由于前40天的各种刑讯和折磨，身体已虚弱到了极点，浑身青一块，紫一块的伤痕，不敢触碰，肋骨喘气大了都疼，严重失眠，血压持续升高，并出现幻听、幻视，这一个月“休息”，我身上的青肿伤基本痊愈了，但精神上的恐惧却丝毫没有减少，想到受到的陷害和冤屈，总想一死了之。<br />
<br />
　　1997年1月5日，我被押送到道里看守所，在道里看守所，虽然吃住条件差，但生活基本规律化，管教员和犯人没有人打我。1997年春节前夕，我从电视上看到中纪委、省委领导讲话，要求将案件办成“铁案”，严禁逼供信，使我对我问题能实是求是的处理b生了希望。此后，我多次向粱大树反映我国贸城一分钱也没有拿，并要求见一下省委、中纪委、市委或专案组的领导同志反映情况，但均遭拒绝。<br />
<br />
　　五月初，我的办案人换成李伟、龚长久。我还是反覆向他们重申，国贸城我一分钱也没有拿。在他们核实我的材料时，我原来在逼供信情况下讲的很多假口供都一一否定了（包括黄福林的），但唯有张庭蒲的口供一直逼我承认，我明白这麽明显的错案什麽迟迟没否定，是有人故意作梗。<br />
<br />
　　五月下旬，专案组几次提审我取得笔录，但因我拒不承认张庭蒲向我行贿的事实，而陷入僵局。<br />
<br />
　　1997年7月11日半夜，我被从道里看守所提出，连夜押送到北安监狱，我意识到肯定要采取新的更严厉的精神肉体上的折磨，迫使我承认国贸城受贿问题，事实果然如此。<br />
<br />
　　入监后，直接将我押到一个不足两平方的地下室小号里，一只手锁在地环上，外面三伏天，室内却阴冷，潮湿，满墙溢水，没有任何行李，晚上半卧在地上几块木板上，我只穿一短袖衫，冷得无法入睡，只好缩成一团，熬到天亮，锁在手上的手铐螺纹锋利，卡到肉里，几天手腕子就溃烂了一圈，昼夜疼痛，每天两个小窝窝头，饿得头昏眼花。从第二天便开始发烧，拉肚子，大小便都在马桶里，五天一倒，室内终日酸臭天比，大便时没有纸只好用手指头揩屁股，然后往墙上擦，由于连续拉肚子发烧，身体非常虚弱，心脏病频繁发作。<br />
<br />
　　入监后第二天，粱大树要我交待原市委书记索长有的问题，和我所谓的串供问题，我听后十分好笑，我心里明白，这是根本没有影的事，事情很明显，还是要我承认国贸城的事，只是换了一种折磨，施加压力的办法而已。<br />
<br />
　　我明确向梁、祁两人表示，这是莫须有，我没有什麽可谈的，同时向梁提出抗议，要求给我行李，要去掉手锁地环、我说我是未决犯，什麽给我押到监狱小号？连死囚都有行李、什麽不给我行李虐待我。<br />
<br />
　　梁说：你不老实交待，再给你换一个更差的地方。但在我连续抗议下，第五天给我拿来一套行李，第七天给我换到另一间较乾净的牢房。又过了三天，七月二十一日晚上，突然往我的牢房里送来两名犯人，一个叫杨诚江，一个叫李宝龙（均判无期），李宝龙坐班，我成了唯一被看管的对象，我的日子更加难过。每天早上五点半坐到晚上八点半，稍坐不直便踢打。三伏天牢内三人一小碗水，我经常喝不到，只能要二名犯人涮碗水喝，一天小便两次，五天大便一次，七月二十六日，将我头剃光以施加压力。在提审时，我向粱大树要求，将已决犯和未决犯共押是违法的，犯人殴打我，我要求将我和两个已决犯分开，起码别让犯人打我，但梁都置若罔闻。<br />
<br />
　　粱推说这是北安监狱的事。这显然是谎话，我是羁押的重要人犯，北安监狱一切听专案组安排，我说你同意分开，请你和北安监狱交涉，粱只是笑，果然直到第二天我又招供国贸城的事，才将我与犯人分开。<br />
<br />
　　在北安监狱的四十天里，我没洗过脸，涮过牙，由于饥渴打骂、锁地环，限制大小便，以及威胁等非人的精神折磨。我身体迅速虚弱，各种疾病一齐复发，不断的头痛，发烧，拉肚、两次心脏病发作，风湿发作后，腿肿得提审时只能扶墙走。我预感到，我可能回不去哈尔滨了，因犯人对我说过，这狱中死了也就是一张死亡证明，就连骨头也见不到。我跟专案组看来无法弄清我的案子了，只有到法庭上说。<br />
<br />
　　于是，我决定先活下来，在一次提审时，我对粱大树说，你们别这麽折磨我了，要整死我吧，反正你不愿下手，把你的手枪借给我，我自杀，咱们都解脱了，（这是我当时的心里话）。梁亮出枪套给我看说：老朱，你看枪没了，你别惦念了。<br />
<br />
　　了尽早脱离北安监狱非人的折磨，能够保住命活著回哈尔滨，在7月30日提审时，我对粱大树说，我恐怕难活著回去了，我实在顶不住了。你们不就是让我承认国贸城那个事吗，我全承认，只求你们把我和犯人分开，让我回哈尔滨吧。<br />
<br />
　　粱大树点头说：我早就知道你受不了这个罪，其实3月份要结束就是这点事，你何苦挺到现在，自讨苦吃。这样我第二次承认了国贸城受贿的事。<br />
<br />
　　之后，从七月二十一日到八月二日由粱大树、祁浩宇连续对我取综合笔录。取完后，8月4日又将我和两名犯分开。大约8月18日检察院去北安监狱提审我一次，8月21日，我被子押回哈市道里看守所。<br />
<br />
　　三、公诉人指控我接受国贸城张庭蒲的贿赂，证据不足，矛盾百出，根本站不住脚<br />
<br />
　　所谓我接受国贸城总经理张庭蒲七万元贿赂一案，完全是在严重逼供的情况下，制造出来的假案冤案。<br />
<br />
　　我和张庭蒲除吃过一顿饭（有第三人在场）外，无任何接触，更没收一分钱。法庭上，公诉人出示的证据不足，矛盾百出。<br />
<br />
　　1、指控我接受张庭蒲贿赂的证据是我的假口供，我两次的承认过程都是在严厉逼供的情况下形成的，我多次推翻过供词，结合办案人讲张庭蒲也推翻过（因在北安时让我交待和张庭蒲串供的事实）。因此事只涉及我们二人，我在法庭上要求与张庭蒲对质，法庭以张已被捕予以拒绝，是没有充足法律依据的，我要求合议庭调查我和张口供的形成过程。<br />
<br />
　　2、时间上矛盾百出，在法庭上公诉人宣读张庭蒲的供词，张说他11月17日从财会处提的钱，给我送钱的时间却是12月初，张说他春节前第二次给我送钱，我却说春节后。除此，在一些细节上也有矛盾，如张说在走廊碰上我的，我却说是他自己进我家的等。<br />
<br />
　　3、我交待的口供中，两次受贿的时间，我均不在哈尔滨，1999年12月初，我在北京开经济工作会议，1995年春节后一、二天，我在上海与上海海狮集团谈判，此事可去市政府办公厅查阅大事记即一目了然。<br />
<br />
　　4、在专案组审查期间，我在逼供的情况下，曾承认国贸城副总经理黄福林曾经送过我五万块钱。此事已经专案组查后并正式取了否定笔录。这可以证明我在国贸城受贿问题的虚假性（这两个人向我行贿的情节一模一样）。<br />
<br />
　　5、张向我行贿我受贿的口供写得有悖常理，如张什麽自己有专车，却要打计程车去市政府？如果是怕人知晓，什麽不到我家去，而去警卫严密，进门要登记的市政府办公厅？张送给我钱后也不交待一下，就将装钱的袋放到我的桌子上，他仅凭一面之交，怎麽知道我会收下，如我不收而送到纪检委，他岂不败露，精神正常的人会这样做吗？<br />
<br />
　　我一向人谨慎，仅凭吃一餐饭之识，敢不说一句话旧收下他的７万元钱吗？<br />
<br />
　　6、 1996年10月1日，我出访四国，途经香港时已得知张庭蒲、黄福林等人被捕的情况。国贸城外方老板黄通国通过哈市驻港办找到我，问他侄子黄福林的情况，我还劝他相信省、市政府，如果我们之间有什麽不可告人的勾当，不要说收他们12万元钱，我还会回哈尔滨码？（我在香港有很多同学、下乡战友、朋友和亲人）。<br />
<br />
　　7、关于张庭蒲向我行贿一事，专案组先后两次取过十余个笔录，翻开这些笔录，除两次七万元是专案人员提供，时间是我编的外，很多细节如钱的去向，张到我办公室衣著行等到都一次一样，这是可以证明此事的虚假性。<br />
<br />
　　以上这些在专案审查期间，多次向办案人说过，并多次要求见有关领导，均被拒绝，关于指控我的其他问题，我和我的辩护人在法庭上做了认真的说明和陈述，我不再重覆。<br />
<br />
　　关于我家中查抄了出来的钱，因我不在家，也从不问家中的钱物，我确实说不清（到现在我仍不知道我家中钱的确切数）因来源很多，有工资、奖金、买股票、亲友馈赠、利息、住院时友人送的钱、开业庆典红包、出国时境内外朋友送小额零花钱等实在记不清了，具体每笔多少更说不清，只能够估计，请法庭谅解。 　　我坚决拥护党中央反腐倡廉的举措，我自己热爱我的党，五十年来中心耿耿党工作。尽管我已处于目前境况，我仍对我们的党和国家无比热爱。对我指控的有关问题，凡是触犯法律的，我愿接受法律的审判。但我不能接受莫须有的罪名。　　<br />
<br />
国贸城向我行贿案，影响较大，但并不复杂，只要不带偏见，没有任何个人不可告人的目的会很容易搞清。我深知在岳玉泉把持专案大权的情况下，要把此事搞清难度很大阻力重重，但我相信，我们的国家，我们的党是伟大的光明的。假的永远是假的，不会变成真的。我想我的案件包括国贸城问题会在法庭审理下，做出公正客观、实事求是的处理。<br />
<br />
　　最后我重申一下我的请求：<br />
<br />
　　（一）岳玉泉与我有重大利害关系，他不适宜担任我的专案组负责人，我依法要求岳玉泉回避我的案件。鉴于岳玉泉现在是哈尔滨市委常委、常务副市长、政法委书记，已经超出法庭管辖范围，我要求通过我的律师，将我的申诉送交中纪委、省市委、省政府、省市人大、政协检察院法院、监察局（厅）等有关部门、机构领导同志。<br />
<br />
　　（二）国贸城总经理张庭蒲向我行贿七万元一案的指控是虚假的，请法庭予以调查。<br />
<br />
　　（三）要求有关部门调查专案组的违法办案问题，以保证我的法律权益，维护法律的尊严。因我在法庭上已揭露了岳玉泉及专案组的有关问题，我提请法庭关注我和我家庭的成员的人身安全，及可能以合法、非法手段进行的打击报复。<br />
<br />
　　我对我申诉的每件事、每句话负法律责任。<br />
<br />
　　申诉人朱胜文<br />
<br />
　　于道里在守所<br />
<br />
　　一九九七年十二月二十二日<br />
