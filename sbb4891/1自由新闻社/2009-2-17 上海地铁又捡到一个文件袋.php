送交者: 谋杀朱胜文的幕后黑手是谁？ 于 January 14, 2004 16:13:02:<br />
<br />
作者：哈市纪委知情人 2004年1月12日 <br />
　　备受全世界关注的前哈尔滨副市长朱胜文在监狱中“自杀”事件已经开始进入剥洋葱式的揭底阶段了。作为知情者，我可以百分之百地向世人宣布：朱胜文不是自杀的，而是北京最高层的几个家伙指使其在我们黑龙江的党徒蓄意谋杀的。因此，朱胜文冤案在可见的将来是绝对不可能获得公正的调查和平反，杀人犯决不可能绳之以法。为什么这么说？让足下徐徐道来吧。<br />
<br />
　　引发朱胜文遭到杀身之祸的事件是这样：在1995年的一次政府例行财务审记报告会议上，朱胜文发现哈尔滨市社会劳动保险基金中有2800多万元不异而飞了。朱胜文在追查中发现是当时的哈市主管公检法的副市长、后来是哈市政法委书记的岳玉泉私自动用了这笔钱。朱胜文胆敢调查岳玉泉私自挪用公款，岳玉泉就利用1996年中纪委正在调查的“国贸城案”对朱胜文设计了一张陷害之网。<br />
<br />
　　这里我们得交代岳玉泉的后台老板了。岳玉泉在哈尔滨的铁杆后台是已经被双规的前黑龙江省长、原国土资源部长田凤山。他们两在哈尔滨就是大皇帝和二皇帝之分。岳玉泉和田凤山在北京的最大靠山是现任总书记胡锦涛、原中纪委书记尉健行和原中纪委副书记刘丽英以及中组部副部长李铁林。东北三省，特别是黑龙江和辽宁，那是尉健行起家的老巢，也是尉健行的根据地，针插不进，水泼不进，黑龙江上下都是尉健行的心腹控制一切核心权力部位。<br />
<br />
　　岳玉泉利用“国贸城案”编造了一个诬陷朱胜文的所谓“受贿证据”，它是通过对国贸城总经理张庭蒲进行残酷折磨而制造出来的伪证。因为岳玉泉是“国贸城案”专案组的组长，他有权指使专案组按他的要求办案。岳玉泉取得这些子虚乌有的伪证后，马上请示他的后台老板、原中纪委书记尉健行和原中纪委副书记刘丽英，同时，岳玉泉把自己挪用的2800万中拿出1000万送给尉健行儿子和刘丽英弟弟，尉健行儿子和刘丽英弟弟各得500万。<br />
<br />
