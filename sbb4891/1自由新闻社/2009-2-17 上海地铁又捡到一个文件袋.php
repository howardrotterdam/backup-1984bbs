																											<div id="postmessage_33161" class="t_msgfont"><a href="http://club.cat898.com/newbbs/dispbbs.asp?boardid=1&amp;id=2683794" target="_blank">http://club.cat898.com/newbbs/di ... id=1&amp;id=2683794</a><br />
<br />
文章提交者：网络反腐小组<br />
<br />
前天是周六，也是情人节。我本来约好女朋友度过一个浪漫的周末。但是在人民广场等了她1个小时，女朋友却发来短信说不能来了。我一个人在广场上转悠，一边看着别人卿卿我我，一边分析着这次感情危机以及处理方案。直到很晚，才匆匆走进地铁站，上了最后一班地铁。<br />
<br />
到了莘庄站，我准备下车，看到自己的座位底下有一个文件袋。一定是情人们大意遗落的。我捡起来打开一看，只有厚厚的一叠复印文件。<br />
<br />
回到家里，我重新打开文件袋，发现还有一张光盘，另外，还有一封没有封口的信。<br />
<br />
信是这样写的：<br />
<br />
=======================<br />
亲爱的朋友:<br />
<br />
如果您无意中捡到这个文件袋，麻烦您将此信和相关的文件张贴于国内论坛。2008年有几次腐败案件都是通过网络曝光后，都在网民的舆论监督和强大攻势下得到了有关部门的重视而进行了处理。我希望这次曝光也可以使这个被掩盖的腐败大案得到处理。<br />
<br />
其实，早在一个月之前，我已经将此文件通过有关机构转交到反腐的部门。但是至今没有任何反馈的消息。看来，还是需要亿万网民的参与和网络舆论的监督才可以真正反腐！<br />
<br />
虽然2008年曝光的案件基本上都得到了处理。但是这次却非常不同，因为腐败分子的主角涉及到高层，也涉及到一些社会名人，更涉及到至今依然逃离法网的地方当权者。为了能够将此案尽可能曝光，被更多的人转发，请张贴上网的朋友进行必要的技术处理，或者将一些比较敏感的内容推迟上网。总之，希望网络和亿万普通网民这次再次发挥强大的反腐监督作用，将腐败分子和背后的官员绳之以法，尽可能将公款追回，减少国家的损失。<br />
<br />
一位无意中得到文件的网民。<br />
<br />
2009年2月14日<br />
===========================<br />
<br />
我一夜未睡，看完了所有的文件，也打开电脑看了光盘里面的内容，被情人抛弃的感情危机也忘记了，头脑被这无意得到的文件内容所充满。<br />
<br />
星期天我好好地睡了一整天，晚上我也认真地思考了这件事情。看来文件的获得者似乎还对网络以及网民的反腐没有足够的信心。说老实话，虽然本人是一个有着10年以上网龄的老网民，但是这次是否可以成功反腐，我也没有信心，因为这个案件的主角不是一个普通的人物，她涉及到地方官员。不过，共产党能够将北京和上海的腐败的市委书记陈希同和陈良宇都拉下马，这个有着特殊社会关系的腐败分子，以及她背后掩藏的腐败官员们，真的有能力可以左右共产党的反腐制度，动摇共产党的反腐决心吗？我也只有拭目以待了。<br />
<br />
于是，我重新注册了一个网名：“网络反腐小组”，决定将我所得到的相关文件和图片上网，。因为此事还有很多疑点和内幕，我希望所有关注网络反腐的网民们继续关注，也发挥“人肉搜索”的强大功能，让此事以及涉及到此事所有腐败官员早日大白于天下。<br />
<br />
2009年2月16日<br />
