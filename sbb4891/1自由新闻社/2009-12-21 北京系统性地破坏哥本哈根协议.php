																											<div id="postmessage_386126" class="t_msgfont">　　　　　　　　　　北京系统性地破坏哥本哈根协议<br />
<br />
<br />
　　历时两周的哥本哈根气候变化峰会最后以一个挽回面子式的空头协议结束，这个结果受到广泛的批评。据参加核心谈判的代表透露，北京在整个谈判过程中一直在阻挡达成任何有实质意义的协约。欧盟的官员揭示：会议一直在北京采取战术中有系统的破坏中进行着，刚开始人们都被误导着一直相信他们希望达成协议。<br />
<br />
　　据英国《独立报》报导，阻挡主要表现在两点：一是作为协议具有法律约束力的最关键部件――各国承诺减排时间表在北京的要挟之下被剔除；二是得到各国支持的2050减排目标――即到2050年全球减排50%，但发达国家在同一时间必须减排80%，在北京的反对下也不得不退出协议。而这个目标被认为是将全球气温上升控制在2摄氏度临界点之内必不可少的因素。<br />
<br />
　　有多名参与整个核心谈判过程的代表和国家元首见证了北京对这次峰会有预谋的破坏，世界各国对北京破坏这次峰会的系统性和过程中所使用的战术感到震惊。一名与会的国家元首表示：这样的中国对本世纪――我们这个星球的未来预示着什么，这令人感到难以置信的忧虑。<br />
<br />
<br />
起草协议的国家元首：房间里充满了愤怒<br />
<br />
　　一位参与起草最后的协议书的国家元首，给出了他亲眼见证的惊人内幕。他说：“在房间里有25个国家的元首，这时大约是星期五晚上6点钟。在我右边的角上有奥巴马总统，然后是布朗坐在边上，另一边是埃塞俄比亚总统，墨西哥总统，巴布亚新几内亚总理……”<br />
<br />
　　“如果中国没有在这个房间里，那么将会诞生一个每个人都想开香槟庆贺的协议。然而这正是中国想做新兴‘超级大国’的第一个迹象，它对全球管理毫无兴趣，对影响它的政权的多边治理毫无兴趣。你可以说整个过程它都缺乏参与性，它玩游戏比任何人都聪明，他们比美国会玩的多。”<br />
<br />
　　“阻挡一件事情总是比推动或做到容易。美国也许有一些责任，因为那是所有背负压力的团体的固有说法――那就是富国不对，他们没有提供足够的钱或他们没有创造足够的减排目标。但事实是，我就在这个会议上，我很清楚‘附件1’中规定富裕国家到2050年减排80%的目标，每个人都支持，唯独中国反对。这项协议最后淡化成现在这个样子，是因为中国不会接受任何国家的任何种类的任何减排目标，不管是它自己或别人。具有法律约束力的关键内容也被中国从协议中剔除了。房间里充满了愤怒，虽然每个人的愤怒都被控制着，但这种气愤的感受是非常、非常清楚。<br />
<br />
　　“中国的代表感到高兴，因为两种方式他们都会赢。如果谈判崩溃他们会是赢家，因为他们无需做任何事情，而他们知道，富裕国家将受到非难；如果谈判不垮掉，因为每个人都如此绝望，这个协议就要淡化到毫无意义，他们还是得到了他们想要的结果。无论哪种方式，他们都会赢。我认为到那个阶段，所有其他世界领导人都知道怎么回事了，只是他们除了愤怒不能做任何事情。”<br />
<br />
　　“这真是让人意想不到的，令人感到难以置信的忧虑是：（这样的中国）对本世纪我们这个星球的未来预示着什么。中国的力量不会削弱，如果是这样的行为方式，那么我们就真的麻烦了。”<br />
<br />
<br />
欧盟官员一：运用战术系统破坏<br />
<br />
　　欧盟一名参与谈判的高级官员表示：中国有系统的破坏了哥本哈根峰会。因为北京对国内日益飙升的碳排放量要面临一个具有法律约束力的削减目标感到恐惧。中国是世界最大的污染者，在哥本哈根世界各国代表都见证了北京对这次峰会的破坏，并对其破坏的系统性和过程中所使用的战术感到震惊。<br />
<br />
<br />
欧盟官员二：剔除减排时间表和50-50、50-80目标<br />
<br />
　　另一名参与整个谈判过程并目击中国行为的欧盟代表证实：在核心谈判中中国有意阻挡的行为，在各国代表团引起广泛的愤怒。<br />
<br />
　　各国最后在哥本哈根形成的应对全球气候变化协议因过于软弱而广受批评，对于关注气候变暖的人们来讲，这个协议本身就是在毁灭希望。尽管各方都接受了，但毫无约束力的“哥本哈根协议”不足以应对气候变化，好多环保人士指责奥巴马没有作出更大的减少二氧化碳排放量的承诺。<br />
<br />
　　然而，作为协议具有法律约束力的最关键的部件――明年承诺减排的时间表，在最后时刻被中国“坚决要求”从协议中撤出。北京威胁如不拿出时间表，就不同意达成这项协议。<br />
<br />
　　另外一个在北京坚持下被迫从协议中去掉的关键内容是：全球到2050年二氧化碳排放量减少50%，其中发达国家减少80%的声明。这项声明被认为是将全球气温上升控制在2摄氏度临界点之内必不可少的因素。<br />
<br />
　　据悉，对于“50-50”（到2050年全球减排50%）和“50-80”（到2050年全球减排80%）的目标，20国集团都已接受，其它各国领导人也已经接受，包括英国总理布朗、德国总理默克尔、法国总理萨尔科齐和澳大利亚总理陆克文。据说，当这些领导人看到中国的要求的时候，感到非常吃惊，尤其是关于发达国家的目标。这名欧盟官员说：“中国认为到2050年他将成为一个发达国家，所以他们不希望限制他们的成长。”<br />
<br />
　　由于经济的迅速扩张，中国已经超过美国成为世界上最大的二氧化碳排放国，虽然刚开始北京在会议的一项国际文书中同意限制其排放量增长，但临到落实具有法律约束力的时候，它突然变的绝望似的。这名官员补充说：“这次会议一直在中国有系统的破坏中进行，而且北京采取了战术，令人费解的刚开始我们都被误导着一直相信他们希望达成协议。”<br />
<br />
<br />
英国大臣：反对减排目标、反对有法律约束力的协议<br />
<br />
　　领导英国谈判的英国能源与气候变化内阁大臣米利班德（ed miliband）昨晚（12月19日晚）说：“令人失望的是中国坚持反对全球排放量减少50％的目标，令人失望的中国不支持任何具有法律约束力的协议。我觉得这两项都是必要的。”<br />
<br />
<br />
目击者：历史之手无处可见<br />
<br />
　　目击者chris green表示：在一片嘈杂和混乱之中，我们徒然等待着历史之手落在我们肩上。哥本哈根会议是多彩的，嘈杂的，也是令人困惑的。但是，近两周的谈判之后大多数人想要摆脱的感觉可能就是强烈的失望和泄气。<br />
<br />
