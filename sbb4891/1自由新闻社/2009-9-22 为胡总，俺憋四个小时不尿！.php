憋尿可引起尿路感染医学上认为，正常人尿道口周围都有细菌寄居，虽然这些细菌经常可以进入膀胱，但并不都引起尿路感染，主要是因为尿液可以冲走绝大部分细菌，同时尿路粘膜也有杀菌能力。如果长期憋尿，尿液无法将细菌冲走，大量细菌在尿路聚集，就可能引起尿路感染。有研究调查表明，尿流不通畅者，尿路感染的发生率较正常者高12倍。不要小瞧尿路感染，尿路感染可能引起严重的并发症，如肾乳头坏死、肾周围脓肿等，甚至导致肾衰竭，引起生命危险。实际上，<strong>在临床，的确曾经出现过因尿路感染而引起肾衰竭直至死亡的病例。</strong> <br />
