The implications go well beyond China's borders, strategists warn. As Beijing's power grows, it will be less inclined, not more, to uphold the current regional order in Asia. In a recent study of 100 recent articles by more than two dozen of China's top strategic thinkers, I found that four of every five articles spoke of circumventing, reducing, or superseding U.S. power and ideas in Asia. China views the liberal order as one designed to preserve American hegemony in the region. Even if Beijing has so far benefited enormously from rising up within the existing order, it might not be so friendly to it once it's risen far enough.<br />
<br />
Washington's strategists cannot prevent China's rise, nor do they want to. But they do argue that the country's strategic ambitions must be constrained, especially in Asia. That will mean enmeshing China in the regional hierarchy that is underwritten by U.S. alliances with Japan, South Korea, Australia, Thailand, the Philippines, Singapore, Indonesia, and increasingly, India. Acutely aware of this existing dynamic, China prefers to deal either with Washington or Asia -- never both.<br />
<br />
This is why the &quot;G-2&quot; approach to U.S.-China relations is more dangerous than reassuring. Strategists fear that such a top-level dialogue of equals, discussing non-economic issues such as security and regional institutions, would offer Beijing precisely the strategic billing it desires from Washington -- with little in return. Already, U.S. allies in Asia -- basically the whole region minus Myanmar, North Korea, Cambodia and Laos -- fear that U.S. leverage over Beijing will be too easily given away.<br />
<br />
As advantageous as functionalists' win-win U.S.-China cooperation may seem, the hidden loser may well be Asia, where most states see the current U.S.-led power structure as the best guarantee of continued peace and stability. Rushing into a G-2 approach - the kind that Washington functionalists prefer -- would jeopardize much of this.<br />
<br />
Unfortunately, so long as economic matters dominate, functionalists may well win the day. Others in Washington and throughout Asia can only hope that too much U.S. leverage on China is not whittled away in the meantime.</div>

							
							
							
							
