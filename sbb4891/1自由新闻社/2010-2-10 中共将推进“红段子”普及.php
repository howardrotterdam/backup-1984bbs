													<h2>中共将推进“红段子”普及</h2>
						
						
																											<div id="postmessage_474869" class="t_msgfont">一场名为“红段子现象――网络时代的中国文化精神和产业走向”的座谈会，在农历新年临近之际，为此拉开序幕。其受重视程度，从参会人员可见一斑：工业与信息化部副部长奚国华、国家广播电影电视总局副局长胡占凡、中国移动副总裁李跃、中国联通副总经理姜正新、中国电信副总经理孙康敏。<br />
<br />
这被视为是国务院给“三网融合”明确时间表之后，几个部门的首次集体亮相与行动。<br />
<br />
在会上，除了发布《“红段子”现象：网络时代的中国文化精神》之外，会议的组织方、中国移动通信联合会执行副会长谢麟振提出建议：与会单位共同组建宏观网络文明（“红段子”）产业发展中心。未来重点培养和发展“百万种子选 (写)手”和“千万群众选(写)手”，实现“亿万群众转发参与”。<br />
<br />
谢麟振也是原信息产业部电子信息产品管理司副司长。他告诉本报记者，成立宏观网络文明（“红段子”）产业发展中心有一套完整的方案，而且筹划多时。要“在中央宣传部和工业和信息化部、国家广播电影电视总局、文化部、新闻出版署的共同领导下，中国移动通信联合会联合与组织移动通信运营企业、移动通信内容开发企业共同组建”。“古有唐诗三百首，今有短信红段子。”他说。<br />
<br />
之所以力推红段子，其间意义，他极为自豪地告诉本报记者：亿万群众参与的短信“红段子”文化是记录与传承中华复兴盛世的最佳形式。<br />
<br />
这一说法是针对之前一个报道的定位：红段子掀起中国的红色新文化运动。这个报道甚至将其意义牵连到五四新文化运动。<br />
<br />
推广者希望，“亿万民众”都加入到红段子创作中。　图 /南方周末资料图<br />
<br />
<br />
用“红段子”抵制“黄段子”“灰段子”<br />
<br />
所谓红段子，原是指区别于黄段子、灰段子的手机短信。<br />
<br />
这个意义在中国移动通信集团广东公司（下简称“广东移动”）总经理徐龙这里还得到升华。他撰文称：具有先进性、思想性和知识性，内容健康向上，形式短小精悍，效果催人奋进的短信，就是“红段子”。<br />
<br />
广东移动是“红段子”最开始被有意识的进行开发的滥觞地。<br />
<br />
自2005年起，广东移动连续五年进行红段子创作大赛。广东移动以软广告与硬广告协同用力，与《南方都市报》联手，推出每日红段子专栏刊发获奖作品，并为优秀的段子作者提供丰厚奖品。<br />
<br />
2006年，广东移动称要打造“亿言堂”，将范围扩大到彩信、炫铃，并且开始实行“收益分成”。在随后的几年里，广东移动还进行了红段子的网络创业大赛。<br />
<br />
红段子的开发，被品牌管理研究者钟超军视为广东移动营销策划中值得称道的一个大手笔。这一创意直接培育了原本并不被广泛接受的彩信、炫铃等增值业务市场。<br />
<br />
更多的社会价值赋予其上。“‘红段子’发多了，就变成一种舆情，对我们的主流社会文化思想就能产生正面积极的影响，慢慢形成对‘黄段子’、‘黑段子’等不良信息的一种矫正。”江门市委常委黄悦胜总结说，从江门“红段子”的发展情况来看，“红段子”已经成为占领主流文化阵地的一种有益的探索和尝试。<br />
<br />
而在2月4日的“红段子现象――网络时代的中国文化精神和产业走向”座谈会上，工业和信息化部副部长奚国华则表示，“红段子”需占据手机与互联网高地。<br />
<br />
“红段子”并非广东移动的专利。“红段子”很早就“红杏出墙”。在重庆、江西、山西，有“祝福祖国”文明公益短信传递活动基地的“红色文化”建设；在华东的宁波和杭州，“和段子”“金段子”等系列活动在开展。<br />
<br />
而中国移动内蒙古公司联合共青团内蒙古自治区委员会、内蒙古自治区精神文明建设指导委员会办公室、内蒙古自治区未成年人保护委员会办公室，则开展了绿种子文明短信大赛。称：“南有红段子，北有绿种子。”<br />
<br />
据了解，从2005年开始，中国移动的“红段子”活动，从广东拓展至重庆、浙江、上海、福建、陕西、山东、河南、云南、甘肃等全国多个省市。<br />
<br />
在广东高校网站联盟庆祝时，主办方甚至在广东高校范围内发起了一个“痘段子”的活动，向参赛者赠送高校祛痘吧系列产品。<br />
<br />
在后来，“红段子”在内容上很大一块实际上是“打造绿色的移动文化”。2009年中国移动“深入开展‘绿色行动计划’，共建绿色通信产业链”实践还进入国资委“中央企业优秀社会责任实践”，使中国移动成为惟一一家入选优秀社会责任实践的电信央企。<br />
<br />
就在中国移动发育红段子的时候，“红段子”成为地方政治中的“一朵奇葩”。<br />
<br />
2008年1月16日，河南省南阳市纪委监察局在《南阳日报》上倡议广大市民用“红色短信”来抵制眼下流行在社会上的各类“黄段子”、“灰段子”。<br />
<br />
2009年4月28日，重庆市“2009年第二届红色短信创作传播大赛”拉开序幕，命名为“红言颂”。<br />
<br />
“红色”大势之下，2008年中国移动通信联合会开始谋划一本书，想以此来为整个计划奠基，并为之命名《红段子现象：网络时代的中国文化精神》。<br />
<br />
这个成立于2000年9月16日具有法人资格的全国性社会团体，信息产业部指导，民政部注册登记。成员包括中移动、中联通、中国电信等主要运营商。会长为中国社会科学院原副院长刘吉。常务副会长为国务院研究室工交司原司长郭振英，谢麟振为执行副会长。<br />
<br />
“红段子”的中国定位<br />
<br />
《“红段子”现象》一书中赋予红段子的价值，远非占领黄段子、灰段子充斥的手机市场这么简单。出版者把它定位为中国的文化精神再造。<br />
<br />
出版此书的人民出版社主要出版马克思主义经典著作，党和国家重要文献，党的路线、方针、政策的普及性读物，党史和党建论著，政治、哲学、经济、历史、法律、文化、国际问题等方面的一流学术著作，以及重要人物传记和哲学社会科学工具书及教材等。被称为国家出版社。<br />
<br />
红段子，则被类比于美国大片、日本的动漫、韩国的电视剧，寄望于“外国人见面谈天气，中国人见面讲段子”。谢麟振说，古有唐诗三百首，今有短信红段子。它也被类比于中国的《三字经》，因为《三字经》是中国最早的“红段子”。<br />
<br />
“红段子”亦被赋予了国家历史使命。《红段子现象：网络时代的中国文化精神》主编之一的谢麟振先生也在研讨会上发言说：“亿万群众参与的短信‘红段子’文化是记录与传承中华复兴盛世的最佳形式。”<br />
<br />
在2月4日的“红段子现象――网络时代的中国文化精神和产业走向”座谈会上，全国政协委员，九届全国工商联副主席、秘书长程路发言称：目前，中国的经济总量已经“坐二望一”，手机用户已经是7.3亿，互联网用户比美国人口还多8000万。“但我们没有发言权，整个世界互联网的信息量流通，2/3是美国人，我们只有0.01这么一个可怜的数字，整个互联网是掌握在美国人手里。很多信息上网之后，美国先看一遍然后再走向该去的地方。”红段子系统工程一方面是为了解决国内的问题，包括“贾军鹏，你妈妈叫你回家吃饭！”这样的无厘头与种种恶搞。而另一个方面，程路说，则是因为“我们的小青年不知不觉的落入了美国文化入侵的陷阱”。<br />
<br />
其志气，从中国移动通信联合会的刊物《中国移动通信》上的一篇文章可见一斑。在报道韩国首尔召开的“中日韩移动互联网高峰会”上中国移动通信联合会秘书长倪建中的讲话时，标题即为：《“汉文化圈”：东亚国家移动互联网发展的基本大纲》<br />
<br />
而在《红段子现象》一书《网络时代的中国文化精神》中说，“我们应重拾汉唐盛世的那种文化自信和巨大影响力，创造网络时代风雷激荡的文化历史。”<br />
<br />
序中引用台湾歌唱组合S.H.E《中国话》的歌词，云：“全世界都在学中国话，孔夫子的话，越来越国际化。全世界都在讲中国话，我们说的话，让世界都认真听话。”<br />
<br />
红色新文化运动<br />
<br />
“红段子”如何可持续发展？至少广东移动对此毫无疑虑。其秘密隐藏在《“红段子”现象》一书的封面：“家书抵万金，短信只一毛。”<br />
<br />
本报记者所获得的一份操作表明：中国移动从红段子中是受益匪浅的。<br />
<br />
经济上的收益来源于大量人的参与。据统计，广东“红段子”活动的群众参与规模已经超过5000万人次，其中广东移动参与用户已超过1700万人次。此外，广东省转发“红段子”总量超过15亿条，其中广东移动用户转发量超过8亿条次。<br />
<br />
而据国家信息产业部发布的数据显示，近年手机短信业务呈几何倍数增长：2000年全国的手机短信发送总量仅10亿余条，到2004年已是2177亿条，5年增长217倍。到2006年，这一数字又刷新成了4300亿条。<br />
<br />
来自媒体的报道称：一则“中国之崛起，民族之振兴，你我齐手创，共建辉煌天”的段子被移动用户相互转发了15万次之多，被称为当年的“最牛”红段子。<br />
<br />
从2006年起，广东移动将“响我所想”红段子由短信拓展到彩铃、彩信、动漫等多媒体。广东移动建立了一个手机用户公开发表作品，人人都可以参与投票的平台（<a href="http://www.hongduanzi.com" target="_blank">www.hongduanzi.com</a>）。而且，推出的网络创业大赛，建立了移动用户上载所产生的信息费全免、下载收益原创者50%分成，转发通信费10%、20％分成，从而鼓励大众参与。<br />
<br />
转发下载收益分成模式的建立，使红段子有了广泛的参与，并带动彩信、炫铃等增值业务走入寻常百姓家。<br />
<br />
钟超军分析说：这个业务商业模式构建的巧妙之处在于，它改变了中移动原来与一些SP签订音乐、短信等共享下载版权收益的协议，通过彩铃/彩信创作大赛，吸引真正的内容原创者眼球，跳过SP们直接与原创作者签订版权收益协议，一方面扩大了自己的版权收益比例，一方面通过大赛征集的大量原创作品充实了彩铃/彩信的内容，一方面又通过大众参与和口碑传递消除了彩铃/彩信与大众之间的距离感，可谓一箭三雕。<br />
<br />
网络创业大赛从2006年10月18日正式启动，至2007年3月30日止，共收到彩铃、彩信、动漫参赛作品3，155，438件，用户投票超过700万，一级传播量超过5300万。<br />
<br />
而广东移动方面称，到2009年底，网络创业大赛已诞生448位周奖和月奖获奖者，有超过693人获得500元以上收益分成奖，380位创作者获得1000元以上收益，85位创作者获得5000元以上收益，27位创作者更是获得万元以上收益。<br />
<br />
分成之外是奖励，2009年，创作手机网络作品，最高奖金十万。<br />
<br />
而在重庆，重庆市委为“红言颂”专门召开会议发动并制订奖励措施：每天从民众发送的原创红色短信中，选出50条好短信，给予10元返费奖励，再从中选出3条最佳短信，给予100元返费奖励。在4月28日至5月13日短短的16天里，全市红色短信累计上传12391条，审核通过5724条，累计转发 478737条，累计参与人数170678人次。其中，薄熙来首创首发的那条“红言短信”，以1600万次转发量而荣登榜首，重庆1300多万手机用户全都收到了。<br />
<br />
2008年3月3日，在回答李长春群众创作红段子积极性如何的时候，徐龙即说：事实证明，红段子的宣传对正常的市场开发毫无影响。<br />
<br />
在第三届中国实战营销高峰论坛暨2005中国十大营销事件、人物评选颁奖典礼上，中国移动广东公司“红段子”大赛就获得中国十大营销事件奖，中国移动广东公司总经理徐龙被评为中国十大营销人物之一。<br />
<br />
不过，正在谋划中的宏观网络文明（“红段子”）产业发展中心将如何收益与分配，鉴于整个详细的方案还在审批过程中，本报记者未能得到明确的回复。一位业界人士告诉本报记者：中央着力推行，经费不在话下。<br />
<br />
先破后立,意在“主流话语权”<br />
<br />
2月4日的“红段子现象――网络时代的中国文化精神和产业走向”座谈会表明：中国移动通信联合会正在组织谋求全国范围内“红段子”的主导权。<br />
<br />
在谢麟振提出共同组建宏观网络文明（“红段子”）产业发展中心倡议后，与会的工信部、广电总局官员纷纷予以赞同。2月8日，就在记者采访期间，相关的会议被紧急召开，以至于谢麟振“忙得连饭都顾不上吃”。<br />
<br />
原本由地方政府和移动运营商所做的工作，已经上升到中央的层面，而这可以视作“手机网站集中清理整治行动”形势发展的结果。<br />
<br />
奚国华在2月4日的会议上表示，手机网络治理第一阶段的治标工作已基本完成，截至2010年1月底，三大电信运营商共计暂停业务合作推广渠道460家，关闭、屏蔽手机涉黄网站1.9万个。<br />
<br />
11月3日，国资委在京首次召开的中央企业社会责任工作会议上，国资委主任李荣融强调：“中央企业要从战略的高度认识企业社会责任。”<br />
<br />
中国移动通信联合会运营总监段沁萍事后向记者解释，中国移动通信联合会出版《“红段子”现象》一书的一个原因，也是为了展现之前各运营商在相关方面所做的工作。<br />
<br />
实际上，按照中央部署，在2010年，关闭、屏蔽手机涉黄网站只是的三步中的第一步。<br />
<br />
2009年12月24日全国工商行政管理工作会议上，国家工商行政管理总局局长周伯华对2010年市场监管工作进行部署时，也表示监管互联网是工作的一个重点。<br />
<br />
与此相应的是，到2009年底，全国实现96%的乡镇通宽带，91%的行政村能上网。<br />
<br />
在专项整顿推出后，“红段子”被视为是“破”后“立”的主要内容。<br />
<br />
奚国华在座谈会上表示，“塑造健康向上的手机网络文化，要疏堵结合、治理与建设并重。”<br />
<br />
在官方表述中，对于“治理与建设”有如下解释：“网络时代的文化精神处于非正常化的迷茫阶段。而此时的主流媒体，在草根崛起的开放的互联网时代却似乎逐渐丧失主流话语权”；“关于主流文化价值观的“缺位”，党和政府以及社会理性力量都在努力探索。事实上，主流文化的缺位，不仅是话语权的缺位，也是市场的缺位。在探讨如何解决主流文化价值观“缺位”问题的进程中，“红段子”以独特的身份登临了网络文化舞台。从它产生的来源来看，它是由信息服务企业推出的一项品牌业务，不仅完全符合市场运作规律，而且完全符合大众传媒的要求。这项业务在内容上，第一次在网络媒体上将主流文化鲜明化、主题化、系列化”。<br />
<br />
而且，相应的契机是，1月13日国务院为“三网融合”列定了时间表。此举旨在打破电信运营商和广电运营商在视频传输领域长期的恶性竞争状态，融合后各大运营商将在一口锅里抢饭吃。而“红段子”，则为工信部和国家广电总局联手推进网络时代的新媒体文化与产品的创新提供了机会。<br />
