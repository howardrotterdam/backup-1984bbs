很多人都对我们的保卫工作很好奇，不少人问我，<br />
但是每次任务都不同，有些也不能说，不过呢，<br />
最近刚好参与了一次警卫首长的的任务，把近期我们的保卫工作的一些新变化介绍给大家。<br />
希望能满足美言各位朋友的好奇心。<br />
具体涉及机密的地方有处理和变更，请谅解。<br />
一般来说，中央常委级的领导出行是有车队护送的，我说的是护送车队，不是其本身的车队。<br />
首长一般出行是乘坐丰田考思特面包车。也就是四川产的那种。。<br />
以前都是进口的，最近好象国产的更多了。<br />
