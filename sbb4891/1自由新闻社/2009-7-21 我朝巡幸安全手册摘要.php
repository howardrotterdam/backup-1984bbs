不过大家能猜到的是加强制动性能和防护。对轮胎和玻璃更换，底盘加固。<br />
我们举例说明一个保卫流程：<br />
如果首长到地方上来，如果是到某省的某市的某区调研或者考察，<br />
一般，事先一个月左右开始准备。半个月通知下到省里。<br />
省里主要规划路线，并且有电话到沿途各地的警方，对路面状况进行维护修缮及保养。当然地方上接到这个任务的时候，一般会把沿路的绿化和亮化顺带搞一下，<br />
所以，有时候不在重大节日的时候 ，大家也会看到，路边有很多人在摆盆栽。。。。。这就说明有大人物要来了。<br />
同时进行的还有入住的宾馆选择问题。一般不会劳烦地方的商业酒店的，即使你是5星的，一般也不会选择，<br />
主要都是选择在政府的招待所（这可不是一般的招待所）或者干部疗养院。<br />
地方上要上报接待方案，按照不同等级的接待任务来安排。<br />
正式出发前一周，省里会派工作组到地方上来检查接待场所。主要是金属探测器和扫雷（呵呵，中国好象没必要，但是这个必须按程序走。）<br />
还有就是对消防和人防工作的重点排查和对接待人员的政治审核。比如服务员等人。<br />
如果省里觉得地方上的接待人员素质不高，一般会直接从省接待的服务员中抽调部分到下面的招待所，贴身服务。<br />
在首长到达该招待所前3天，中央警卫局的人会到现场再次检查指导和排查。<br />
现在这一届政府很注重节俭和减少扰民。因此出发的路途上，一般不准大规模增加警力，<br />
但是上有政策，下有对策。<br />
地方上总想万无一失。所以，还是会悄悄增加警力的，不过很多增加的警力被要求穿便衣执勤。<br />
我参与的这次安全工作，就有地方上的某交警队的头目，不听要求，穿警服在路边指挥。。<br />
车队过后，该人被撤职。<br />
车队在路上有自己的处理原则，一般警车开道，挂的是省里的牌照，在面包车后面还有没牌的小汽车，很不气眼的，私家车一样的，<br />
一般都很新，比如2厢的高尔夫 什么的。这些车是处理突发事件用的，里面都是安全保卫人员。<br />
如果开道车遇到什么状况，不方便警方出面的，由他们负责。但是实际上，没有出现过什么状况，毕竟地方上都早就把工作做到位了。<br />
按照条例假如，车队遇到极端的突发事件，假如，前方有枪声或者炸药爆炸的声音等，车队是严禁继续前行的，必须立刻掉头，原路返回。<br />
知道为什么么？<br />
或许大家会想，冲过去就可以了，这是严重违反保卫条例的。因为，前面的路上，什么状况，没人知道。而后面的路因为已经走过了，相对安全。<br />
因此回去是相对最稳妥的处置方法。<br />
<br />
车身加强装甲等。另外还要内部必须的装修等等<br />
当车队到达指定的接待处的时候，该处的警戒一般分3个层次。分别用3种颜色的胸徽表示。<br />
该胸徽上也不会写北京或者中央什么的，一般只写“某省警卫” 几个字。内、中、外3圈，直径不大，也就200米左右。<br />
但是这些人一般不穿警装，都是便服， 不过那身派头，一看就不是好惹的。<br />
人长的都不错，1米85左右的身高，年轻，帅气。黑色的西装，和西方比 我们戴墨镜的很少。<br />
其实戴墨镜有很多好处，可以让人看不到你眼睛在看哪，增加对方的心理压力，但是中国主要考虑到低调。不要搞的一看就是保镖。。<br />
话说回来，那些年轻人身上的气质，一看就是武林高手。<br />
呵呵。手开5砖是基础。<br />
当然，外围的警戒不是由他们着些人负责，而是地方上的警察负责。<br />
 <br />
那些没牌照的车就停在 招待所沿路的大门外。几个戴徽章的人站在车边，象是聊天。其实都是在注视过往的行人。<br />
 <br />
如果有人要靠近，他们就会上去阻止。相信，看到他们的胸徽，大部分人会很自觉。<br />
如果有人真的天不怕地不怕，非要闯，那么就会被带走。交由地方处理。地方上的领导面子也挂不住。估计这个人的下场不会好。<br />
 <br />
在招待所里，首长住的地方，所有床上用品等东西都是换新的。<br />
至于在房间里活动的时候的保卫工作，涉及机密，此贴不提。<br />
房间里有红色的专线电话，呵呵，首长都是不用手机的。<br />
吃饭，没大家想得那么复杂。什么不吃地方的食品啊，没那回事，到了地方，地方的特产还是要品尝的。<br />
只是厨师要可靠的，如果不确定的，就换厨师。。<br />
一般由该市的书记陪同进餐，顺便介绍菜肴。按一般程序来讲，地方上的其他人 不可能有机会在吃饭的时候，看到首长的。<br />
但是地方四套班子的主要领导，会都在附近，时刻等候接见。能合影就更好了。<br />
一般这样的出行，地方电视台是不能跟进拍摄的，只有中央台随行。地方上的媒体基本上不知道的。即使是地方的党报党刊。也是领导走了之后很多天，才能发表通稿。<br />
地方上的电视台，想要播放领导在该地的画面的，要花大价钱从中央台买审核过的编辑好的首长在该地活动录象。<br />
地方上的拍摄手法和水平根本达不到这个能力。当然更多也是从保密和安全角度出发的。<br />
 <br />
首长吃完饭，一般厨师和工作人员不得擅自离开。起码要4个小时之后，才可以自由一点，为什么，因为如果腹泻，4个小时肯定出状况了。<br />
如果4小时还没腹泻就不会再腹泻了。<br />
其实当厨师知道今天为大领导服务的时候，那也是饭做好了，领导在吃的时候，他才知道是谁来了吃他做的东西了。之前是不会告诉他的。<br />
当然，假如首长在此地住了不止一天，那么厨师和服务人员是要换的。不能让你继续接待了。。。。。。。。如果他想做什么都来不及了。<br />
