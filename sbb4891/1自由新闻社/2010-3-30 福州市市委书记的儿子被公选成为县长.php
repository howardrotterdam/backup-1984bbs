4.2 还有一条隐秘的说明“以上职位除共青团福州市委副书记为2名外，其他职位均为1名。”<br />
<br />
5.《福州公开选拔副处级干部拟任人选任前公示》，公示时间为7天，从2009年10月28日开始。<br />
<a href="http://bbs.hsw.cn/read-htm-tid-1698803.html" target="_blank">http://bbs.hsw.cn/read-htm-tid-1698803.html</a><br />
