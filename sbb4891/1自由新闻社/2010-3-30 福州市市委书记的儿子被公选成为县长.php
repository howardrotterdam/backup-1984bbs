<br />
5.1 从高分到低分选取综合成绩前35名共36人(因第35名有2人并列)，<br />
公示对象(按综合成绩名次排列)：<br />
<br />
5.2 第2名：<br />
雷连鸣，男，畲族，1983年8月出生，福建福安人，中共党员，研究生学历，经济学硕士，现任中国人民财产保险股份有限公司福州市分公司党委委员、副总经理。 <br />
主要简历：2005年7月至2006年9月在团中央工作，任全国学联副主席、驻会执行主席，2008年7月于清华大学金融系应用经济学专业研究生毕业后留校工作，2008年7月起任清华大学研究生团委副书记；2008年9月至11月挂职任中国人民财产保险股份有限公司福建省分公司财产保险部副总经理；2008年11月起任福建省分公司财产保险部副总经理；2008年12月起任福州市分公司党委委员、副总经理(挂职)，党委委员、副总经理。<br />
<br />
5.3第5名：<br />
苏建，男，汉族，1978年9月出生，辽宁开原人，中共党员，研究生学历，哲学博士，现任福建省纪委办公厅综合处主任科员。 <br />
　　主要简历：2003年8月至2004年8月在团中央工作，任全国学联副主席、驻会执行主席；2008年7月于东北大学科学技术哲学专业博士研究生毕业后参加工作并任现职(其间：2009年5月至今挂职任长汀县纪委主任科员)。<br />
<br />
6. 《福州市公开选拔35名副处级年轻干部任职人选公告》<a href="http://61.154.9.156:81/gxgg/200911/t20091121_63658.htm" target="_blank">http://61.154.9.156:81/gxgg/200911/t20091121_63658.htm</a><br />
苏 建同志任共青团福州市委副书记；<br />
雷连鸣同志任共青团福州市委副书记；<br />
<br />
好了，材料就这么多，下面咱们就分析分析其中的奥秘，看看是不是有 “雷连鸣，你妈雷春美喊你回福州当团委副书记！”这档子事儿。<br />
<br />
从资料中可以看出，雷连鸣2006年从厦门大学金融系免试推荐到清华大学读金融专业硕士研究生。清华大学的硕士是三年制，按说毕业时间应该是2009年6月前后毕业，可是从简历上看，是2008年毕业的，提前一年完成学业。<br />
<br />
于是第1个问题产生了：为什么要提前一年毕业呢？<br />
 因为报名条件中有这么一条“上述任职时间计算至2009年8月31日”，这是死线。看明白了吗？这就是为什么雷连鸣2008年7月必须要从清华毕业，而且马上在当月（2008年7月）就上任清华大学研究生团委副书记的真正原因，时间不等人啊！<br />
 当然这一切绝对离不开其老妈雷春美的全力运作。首先，至少要提前一年知道这批年轻干部的选拔消息、时间和条件（信息时代嘛，信息最值钱，炒股也是这样）；第二，要提前一年把儿子从清华捞出来，先弄个清华大学研究生团委副书记（这就看本领了，光有信息也没用），凑足12个月的任职时间，条件刚刚好；第三，把人弄到人保福州公司（这个简单），这可是必须的，否则人不在福州怎么参见公开选拔？紧接着是一组眼花缭乱的组合拳，目的是弄个职位以满足选拔条件。一切条件都OK了，剩下的事也就顺理成章了，谁也说不出话来。<br />
<br />
第2个问题在哪里呢？<br />
可以看到，在《福州市公开选拔副处级年轻干部公告》中有一句话“以上职位除共青团福州市委副书记为2名外，其他职位均为1名”，这是为什么呢？<br />
因为本文的主人公要任的职位就是这个副书记，另一名是苏建，其条件比雷连鸣还要硬，也在团中央工作过，也当过全国学联副主席，还是博士，任职时间也长。怎么办？难不倒人家老妈，好办，来个“按综合成绩名次排列”，雷连鸣排第2，苏建排第5就行了。排第2？这可是一个任职仅一年的毕业生，雷人啊！<br />
<br />
第3个问题就更加出神入化了。<br />
最后，还有一个更加诡异的东西就是《福州公开选拔副处级干部拟任人选任前公示》中有一句话“从高分到低分选取综合成绩前35名共36人(因第35名有2人并列)”，靠！这玩意儿还有并列，并且恰恰是最后一名并列，当然了为遮人耳目前面也弄了几个并列的，不能埋没人才啊，36人全要了。<br />
其实真正原因应该是，35人名单早已内定，后来的雷连鸣插进来势必会PK掉一名，这虽然不是什么大问题但是毕竟影响不好，弄不好还会坏事，怎么办？只有让第35名并列了。做的真是天衣无缝，叹服！<br />
也许有人要问既然1年前就运作了，为什么不弄个36名？也许是选拔人数早就定死，已经上报到了，能力所限改不了，只能逼宫了。否则绝对不会是35个，而会是53、153或250都有可能。<br />
<br />
最后的结论就是，36人年轻人，个个儿神通广大。<br />
我说“雷连鸣，你妈雷春美喊你回福州当团委副书记！”不过分吧。”</div>

							
							
							
							
