<br />
　　读者称，雷春美的另外两篇论文《关于民法与劳动关系的几点思考》和《培育发展中国资本市场的战略思考》也涉嫌抄袭，原作均在更早的时间公开发表。<br />
<br />
　　前日，记者在2004年第一期的《福建政法管理干部学院学报》中看到，雷春美的文章发表在第11、12页，文章为单独署名。记者将这篇3000字左右的论文与应松年的文章相比发现，只删去了英文版内容摘要，文章开头有所删减，论文内容没有变化，连小标题也无改动。<br />
<br />
　　论文相似度近九成<br />
<br />
　　雷春美的另一篇论文《关于民法与劳动关系的几点思考》，发表在2004年第一期的《福建金融管理干部学院学报》上，文章也为单独署名，于2001年第三期《社会科学战线》中也能找到，题目是《民法与劳动法：制度的发展与变迁》，作者是吉林大学法学院教授冯彦君，两篇文章相似度近九成。<br />
<br />
　　雷春美在1999年第9期的《金融与经济》上发表的论文《培育发展中国资本市场的战略思考》，也与吉林大学商学院教授宋冬林发表在1995年第4期的《内蒙古财经学院学报》论文相似。<br />
<br />
　　前日，记者就此向雷春美求证，但雷的办公室电话一直无人接听，其秘书称雷本人正在出差。市委宣传部表示将与雷本人沟通，但截至发稿时，雷未回应。<br />
