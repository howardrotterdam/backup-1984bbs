													<h2>福建南平市委书记论文涉嫌抄袭 相似度近九成</h2>
						
						
																											<div id="postmessage_545922" class="t_msgfont">本报讯 (记者贾鹏)近日，有读者发现多位知名学者的专业论文，被同一人抄袭后发表在学术期刊上，抄袭者是福建南平市委书记雷春美。记者昨日向期刊编辑部求证此事，证实了读者说法。多名被抄袭学者表示，截至目前尚未听说论文被抄袭，也从未授权期刊转载，截至记者发稿时，雷春美本人尚未对此予以回应。<br />
