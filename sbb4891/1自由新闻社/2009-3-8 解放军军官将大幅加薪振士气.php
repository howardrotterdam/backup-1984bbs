													<h2>解放军再度加薪提振士气</h2>
						
						
																											<div id="postmessage_60974" class="t_msgfont">坊间流传一年余的中国军队加薪消息日前得到证实，十一届全国人大二次会议期间，中国公布2009年国防预算为4806.86亿元人民币，比上年的预算执行数增加624.82亿元人民币，增长率为14.9%。 <br />
全国人大新闻发言人李肇星解释说，增加军费中大部分将用于中国230万军人提高工资和福利。据透露，中国军人今年加薪幅度较往年有所增加。根据军龄和级别有所不同。一个普通的少尉每月工资有望上涨数百元，而团级干部工资则将上涨大约两千元左右。连职1200上下，营职1500上下。 <br />
<br />
“此次调薪原定于去年年初全军推行，后遇到汶川地震，国家集中财力物力救灾，军人调薪因此顺延至今。”军方人士告诉本刊，给230万中国军队调薪已成定局，但有关调薪幅度和细则尚未出台。中国军人收入与地方同等级别公务员相比，仍有相当差距。此次全面加薪后，部队与地方收入水平有望拉进。 <br />
<br />
中国的军费根据国民经济发展情况合理安排，按占国内生产总值比例计算，作为军费支出中的一部分，军人工资制度的升迁，也随之历经曲折。北京军方一位资深军事后勤研究学者，近日向本刊透露了中国军队工资历次改革演变的奇特经历。 <br />
<br />
&nbsp;&nbsp;<br />
<br />
军队收入也现“大锅饭” <br />
<br />
建国后的1952年初，中国军队普遍评定了军队行政级别，正式建立起军队工资等级制度。 <br />
<br />
根据国防部1951年第1号命令颁发的《薪金律贴暂行办法》及有关文件，从当年1月起，解放军实现了由解放前供给制向薪金制的转变，同时1952年的干部级别也保留了下来。这套制度基本工资由级薪和军龄补助金两部分组成。除基本工资外还建立了一定的补助、补贴政策。初步建立起了工资增长机制。 <br />
<br />
就1955至1964年间军队干部月平均工资的增长情况来看，9年间除了1961、1962年因17级以上党员降薪导致平均薪金水平下降外，其余7年干部平均薪金水平是逐年增长的。 <br />
<br />
由于军官级别不随军人职务的晋升而提高，因而增资渠道单一，主要增资因素是干部军龄的增长。同时，职级脱节对军人工资制度的动态均衡产生不利影响，以至后来出现部分干部职薪脱钩甚至“倒挂”现象。 <br />
<br />
为消除军官过高的薪金标准带来的不良影响，1965年2月中央军委颁发《关于改革军官薪金制度的决定》，从当年6月起废除原来的薪金制度，取消军龄补助金，废除军队干部级别，将军队干部原来的级别改定为国家行政干部级别，工资标准与国家机关干部大体取得一致。 <br />
<br />
中国军队实行行政级别工资制不久，平均主义、“吃大锅饭”等地方常见的弊端也在军队现身。 <br />
<br />
该学者称，就基本工资而言，单一行政级别工资制在贯彻按劳分配原则方面有所倒退。将军队干部级别改定为行政级别，与军队工作特性不符，也导致了干部职务与工资级别的脱节，干部职务不断提升，而工资级别只在1972、1977年做过部分调整，从而使干部工资差别与劳动差别很不相称。 <br />
<br />
“一个正连和副营，或正团和副师干部的工资收入差别不大。虽然军队讲奉献为主，但在报酬上也要考虑按劳取酬和工作资历等因素。”该学者说，那时军队取消军龄补助金，实行单一的劳动报酬，在工资待遇上对资历因素体现不足，不利于鼓励干部安心服役的工作积极性，一些老同志多少也有情绪。 <br />
<br />
&nbsp;&nbsp;<br />
<br />
工资制度的四次演变 <br />
<br />
1980年部分改革工资制度，首次将军队干部的部分工资与职务相结合，在体现按劳分配上前进了一步。部队的专业技术干部首次通过评定技术等级，将其工资与技术水平联系起来，调动了工作积极性。 <br />
<br />
但在津贴、补贴制度改革上，仍很滞后。由于当时树立抓经济建设为主、军队要过紧日子的思想，军队干部工资制度缺乏正常的增资机制。 <br />
<br />
这一时期，军队干部工资制度缺乏正常的增资机制，除1972、1973、1978等少数年份外，军队干部平均工资水平逐年下降。解放军干部1981年的月平均工资只相当于1957年时的水平，比最高时的1961年的水平仍低7. 84%。 <br />
<br />
尽管军人工资低下，但军人吃住免费，每月有适当的津贴工资，加上在中越战争几年中树立的中国军人奉献牺牲的崇高职业形象，仍令地方青年非常向往。 <br />
<br />
1985年7月，中国军队开始实行职务、级别、军龄三结构工资制。由于当时军队干部军衔制度恢复工作正在进行当中，为降低改革成本，作为向后来的职务、军衔工资制的过渡，在适当调整标准的基础上，保留了行政级别工资，而同时进行的国家机关工作人员工资制度改革，则取消了行政级别工资。 <br />
<br />
改革后职务工资在基本工资中所占比重较改革前上升了28个百分点，级别工资则下降了34个百分点。 <br />
<br />
根据现役军官实行军衔制度的新情况，经中央军委批准，1988年10月1日起，解放军军事、政治、后勤军官开始实行职务、军衔、军龄三结构工资制。职务工资按基准职务等级确定，各职级军官均按原行政级别进入职务工资档次;进档后，原行政级别废止。 <br />
<br />
1988年的工资制度改革，取消了级别工资，实行职务、军衔、军龄三结构工资制。该项分配制度在增资机制上仍待完善，但照顾了军人资历、年功贡献因素，较好地体现了军人职业特点。 <br />
<br />
经历了改革开放十年后的中国东部沿海诸省，地方经济发展突飞猛进，相对而言，军人工资收入普遍过低。驻扎在这些地方的部队，出现因收入低军心浮动，出现提前转业的风潮。 <br />
<br />
上世纪90年代初前后，解放军一位中尉连长的每月收入不过700余元，相当于地方外资企业的一名保安工资。参照国家公务员工资制度，解放军于1993年10月起开始实行职务、军衔、基础、军龄四结构工资制。 <br />
<br />
职务工资是基本工资的主体，设若干档次，按干部职务高低、责任轻重设置工资标准。军衔工资是军人荣誉在工资待遇上的体现，按军衔等级、衔龄设置。基础工资是保障干部本人基本生活的费用，与国家基本标准一致。军龄工资是军人年功贡献的体现，标准不变。 <br />
<br />
另外，为增强军人职业的荣誉感，体现军队特点，参照国家公务员每年增发一个月奖金的做法，建立了军人职业津贴。 <br />
<br />
&nbsp;&nbsp;<br />
<br />
有助于提振士气 <br />
<br />
按照国家的统一规定，军队干部工资应高出国家公务员工资的20%，但2002年，军队干部月人均工资收入为1666元，国家机关工作人员月人均工资收入为1607元，军队干部比国家机关工作人员仅高出3.7%，完全没有达到高出20%的标准。 <br />
<br />
如果包括士官，则军队人员(军队干部、士官)的工资不是高于而是低于国家公务员的工资了。2002年军队干部、士官月人均工资收人为1324元，比当年国家机关工作人员工资低约17.6%。因此，调整解放军军官队伍的工资水平势在必行。 <br />
<br />
2006年7月1日，解放军实施了有史以来最大幅加薪。解放军工资增幅约一倍，而中下级军官加幅最高。解放军军官的工资主要由基本工资和津贴两部分组成，其中基本工资由职务工资、军衔工资、基础工资和军龄工资四项组成。 <br />
<br />
这次加薪军龄工资由原来的每年增加人民币1元，调整为每年增加10元，基础工资由原来每月230元调整为600元，军衔工资则平均增加约三倍，职务工资则平均增加约两倍。经过这次调整，原来50元至200多元的生活补贴，将不分职级增加至每月560元。 <br />
<br />
按照新的工资制度，大学毕业后入伍已有四年的解放军中尉级连长，其基本工资可从原来的1200元左右大幅增加到2500元左右；有30年军龄的副师级军官，这次可加薪约2500元左右，基本工资可达5000多元。 <br />
<br />
而今年的调薪幅度虽未公布，但从军队工资改革看，不同军兵、种，不同岗位，不同区域的军人的工资细分、津贴补助的分配会更趋科学合理。按目前已有消息来看，今年军人工资增幅仅次于2006年的那次。 <br />
<br />
“军人工资水平既反映一个国家的经济发展水平，又体现一个国家军人的社会地位。”北京军方那位学者称，在金融危机之年，相应调整军队的津贴补贴标准，保证军人的生活水平进一步提高，体现了党和国家对军队建设的关心和厚爱。 <br />
<br />
“与国家公务员工资结构相对应的军人工资制度，有利于随国家同步调整工资，同时将提振士气，增强军人的荣誉感和军队的凝聚力，另外也有利于解决困扰多年的转业军人的工资套改难题。”他说。</div>

							
							
							
							
