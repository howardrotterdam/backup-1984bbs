面对死刑表情平静<br />
<br />
周晓亚的辩护律师倪泽仁介绍，面对判决结果，文强没什么反应，很平静，周晓亚则情绪激动，当庭失声痛哭，瘫软在地。“一是觉得自己判得重，她的心理预期是5年，主要是没想到文强判死刑”。<br />
<br />
文强未当庭表示上诉，其辩护律师杨旷生表示，是否上诉要看当事人的态度，计划到羁押地会见文强，看其是否要上诉。记者昨晚获悉，文强昨日已表示要上诉。<br />
<br />
法院将对文强犯罪所得2260万余元继续予以追缴。<br />
<br />
(本文来源：中国新闻网 )&nbsp;&nbsp;<br />
<br />
广州日报4月15日报道 昨日16时45分，重庆市第五中级人民法院对文强案进行一审进行了公开宣判。文强当即被判处死刑，剥夺政治权力终身，并没收全部个人财产。<br />
<br />
文强之妻周晓亚，以及手下“三大金刚”黄代强、赵利明、陈涛一审被分别判处8至20年有期徒刑。文强等5人当庭未表示要上诉。不过，记者昨晚获悉，文强律师杨矿生已表示会继续上诉。<br />
