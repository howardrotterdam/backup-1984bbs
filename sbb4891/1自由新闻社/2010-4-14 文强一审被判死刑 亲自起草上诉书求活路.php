													<h2>文强获死刑当晚彻夜难眠 亲自起草上诉书求活路</h2>
						
						
																											<div id="postmessage_566112" class="t_msgfont"><img src="http://pic1984.12hp.de//up/1271391516.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
14日，重庆市第五中级人民法院门口。该院对文强及其手下“三大金刚”黄代强、赵利明、陈涛涉黑案进行一审宣判。<br />
<br />
辩护律师杨矿生及其助理赵铭在看守所会见了刚刚被一审宣判死刑的文强。据了解，文强在被一审宣判死刑的当晚，可以说是彻夜难眠。但是见到律师的时候，文强的精神尚可。昨日，文强亲自起草上诉书。<br />
<br />
判决时感觉像坐过山车<br />
<br />
据介绍，14日上午，法院宣布赵利明送给文强的那幅落款为张大千的青绿山水画是赝品的时候，文强的心情放松了许多，因为这一下子使他的受贿金额减少了 400多万，而且还有几笔受贿金额都未被法院认定。<br />
<br />
相比公诉人的起诉书，法院在一审中，无论在涉案金额还是具体涉案事实的认定上，文强案都有了“瘦身”。 一审中，重庆检方指控文强单独或通过其妻受贿1625万余元(包括78万元“黑金”)，然而，一审最终认定此数额为1211万余元，对414万元不予认定。 <br />
<br />
此外，一审对“文强系龚刚模保护伞”的指控不予认定，缘由是龚刚模两次送现金都是通过中间人，并未与文强直接接触，两人不存在请托事项，来往也不密切。至此，文强这顶“保护伞”下的“黑老大”，由6人变成5人。 <br />
<br />
因此，文强曾一度认为不会判处死刑的，认为自己在庭上据理力争终于有了个较好的结局。所以下午出庭时，他的表情也是较为轻松。 <br />
<br />
然而，当日下午，文强被一审宣判死刑的时候，这一结果超过了文强的心理底线。他虽然表面平静，但是内心十分难受。昨日文强在会见律师时说：那一刻感觉像是在坐过山车……<br />
<br />
文强的妻子周晓亚也认为文强不会判死刑，所以审判长宣判文强死刑的时候，周晓亚情绪失控，失声痛哭。 <br />
<br />
据介绍，14日一审判决宣布后，文强虽然没有当庭表示要上诉，但是被带下审判庭后，他当即表示要上诉。当晚，文强茶饭不思，情绪较为低落，一直在看守所思考着什么。<br />
<br />
看守所亲自起草上诉书 <br />
<br />
昨日在会见律师的时候，文强表示，自己要亲自写上诉书。文强询问了律师上诉书该如何写以及上诉的程序，并当即开始动笔起草判决书。律师说，关押了这么长时间，文强的心理素质也变得很强了。 <br />
<br />
从重庆打黑系列审判结果看，很多二审都维持了原判。如刘虎成维持原判：死刑；杨天庆维持原判：死刑；王天伦维持原判：死缓；黎强维持原判：有期徒刑 20年；谢才萍维持原判：有期徒刑18年……<br />
<br />
文强的一位家属昨日也表示，支持文强上诉。家属表示，二审如果维持原判，还要到最高法院去上诉。昨日，律师会见完文强后，文强的家属一直在和杨矿生、赵铭两位律师闭门商谈，至于什么内容，不得而知。 <br />
<br />
另据记者了解，文强、周晓亚的儿子十分关注父母的审判情况，现在他变得很沉默，和同学、朋友几乎没有什么联系。<br />
<br />
法律界人士分析，文强如果希望二审得到从轻处罚，就得进一步找出新的证据，减轻自己的罪名，如能有新的立功表现也是减轻处罚的路径。 <br />
<br />
文强的妻子周晓亚就是很好的例子。法院在解释周晓亚量刑较轻的原因时说：周晓亚虽然受贿数额达449万余元，但其受贿是在司法机关尚未掌握的情况下主动交代，系自首，具有法定从轻、减轻处罚情节，且认罪态度好，有悔罪表现。 <br />
<br />
原打算坐牢写回忆录 <br />
<br />
记者从有关方面了解到，文强本来做好了坐牢的准备的。 <br />
<br />
一位在文强受羁押期间接近文强的重庆政法界人士说，文强最初被羁押的时候态度极为张狂，面对办案人员的讯问一律不予理睬，但是仅3天后，就变得特别地配合办案人员。<br />
<br />
此前，重庆市公安局局长王立军也曾对媒体记者说过：“文强这个人长处很多，是一个很熟悉业务的干部，而且这些年，做了很多贡献。”在看守所，文强与相关人员谈心的时候曾经表示，自己从警30多年，破获了很多大案要案，如劫匪张君案。 <br />
<br />
文强求生欲望十分强烈，他曾在看守所对人说：如果坐牢，除了反思自己的罪行，好好改造外，还将抽时间写自己的回忆录，将自己的一生作个梳理，写成一本书。 <br />
<br />
文强16岁就开始下放农村，期间很勤快，栽秧、割谷、挑草头、守鸭棚都肯干。后就读警校，毕业后加入警队，29岁时任巴县县委副书记，37岁起担任重庆市公安局副局长达16年，先后指挥破获震惊全国的警匪枪战案、中国第一盗窃案、运钞车被劫案、杀人恶魔张君案，获公安部一等功多次，是闻名一时的“打黑英雄”，直至成为直辖市重庆的司法局局长。没曾想到他最后成了被人唾弃的阶下囚……看来，文强的经历的确值得一写。<br />
<br />
其他4名被告也要上诉 <br />
<br />
记者昨日了解到，除了文强外，文强的妻子周晓亚、文强手下的“三大金刚”：刑事警察总队原副总队长黄代强、公共交通治安管理总队原副总队长赵利明、治安总队原副总队长陈涛，这4人在会见律师时，均表示不服一审判决，要上诉。 <br />
<br />
记者手中的一份重庆市检察院第五分院的起诉书显示，黄代强、陈涛先前还涉嫌强奸罪，后来在实际起诉中没有涉及这一罪名。检察机关解释说，因案情重大、复杂，此案曾被退回补充侦查，黄代强、陈涛的强奸罪名因为事实不清、证据不足而没有被起诉，但是文强涉嫌强奸罪还是被提起公诉。<br />
<br />
昨日，除文强的律师还在重庆外，其他4名被告的律师均已离开重庆，并表示回去做好准备，等待二审的开庭。<br />
