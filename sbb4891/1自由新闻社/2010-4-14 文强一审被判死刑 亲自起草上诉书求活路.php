文强40年：从中专生到正厅级到被判死<br />
<br />
14岁就读四川省公安学校，29岁时任巴县县委副书记，37岁起担任重庆市公安局副局长达16年，先后指挥破获震惊全国的警匪枪战案、中国第一盗窃案、运钞车被劫案、杀人恶魔张君案，获公安部一等功多次；是闻名一时的“打黑英雄”，号称“全国排名靠前的刑事侦查专家”；他所领导及主管的重庆市公安局刑侦力量一度居全国前列。<br />
<br />
同是此人，坐拥8处豪宅，家藏过亿资产，为至少6个大型黑社会团伙提供保护。在他的任期内，全市1400多起命案未破，500名杀人犯在逃。他又被称为“全市最大的黑社会保护伞”。 据《晶报》<br />
