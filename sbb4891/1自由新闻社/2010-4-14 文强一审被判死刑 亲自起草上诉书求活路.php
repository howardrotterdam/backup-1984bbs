强奸女大学生<br />
<br />
一直有所争议的“强奸罪”是否成立，成为文强案判决的关键。检察机关指控文强酒后强奸一在校女大学生，庭审中文强对此予以否认，当时法院以涉及个人隐私为由，进行了不公开审理。而这次审判，对文强的所有指控罪名均成立，包括强奸罪。<br />
<br />
法院查明，2007年8月28日晚，文强约某女到重庆市渝北区一餐馆和一会所吃饭、唱歌，授意他人劝该女大量饮酒后，将其带至宾馆，不顾其反抗，强行发生性关系。<br />
<br />
庭审直击：<br />
<br />
步履蹒跚到庭 一脸沉静听判<br />
<br />
昨日上午开庭时，法院仅有30多人参加旁听。9时整，文强在法警的搀扶下，步履蹒跚地走向被告席，精神大不如前。文强的穿着还是第一次庭审时的黑灰色休闲夹克衫。记者从法院有关人士处了解到：文强的腿部有些陈旧性的损伤，最近因为天气有点冷，气候有点潮湿，腿部有些隐隐作痛。整个宣判过程，文强一直很沉静，没说一句话。而周晓亚听到宣判结果后瘫倒在地，失声痛哭，被法警架离法庭。<br />
<br />
数度冒雪去北京请来律师<br />
<br />
穷姐姐要卖房助弟打官司<br />
<br />
在被带离法庭时，文强扭头向旁听席上张望，他的大姐就坐在旁听席上，这名六旬老妇伏在椅上失声痛哭，说一定要上诉。据了解，她因周晓亚不好说话相处不来故一直少有来往，下岗后还要出外打工。文强案发后，她到处借钱，并几次冒雪去北京请来律师杨矿生，现在还决定将老房子卖掉打官司。<br />
<br />
审判长：文强的犯罪行为“影响极其恶劣”、“情节特别严重”<br />
<br />
因何判死：罚当其罪<br />
<br />
为什么文强会被判死，审判长释疑：刑法第六十一条规定，对犯罪分子决定刑罚的时候，应当根据犯罪的事实、性质、情节以及对社会的危害程度综合考虑。<br />
<br />
就本案而言，文强以各种名义收受他人贿赂，严重损害了国家工作人员的职务廉洁性，且受贿1211万余元，数额特别巨大；文强身居司法机关要职，长期收受下属贿赂，为他人调动、职务晋升牟取利益，对重庆市公安队伍建设和司法机关的公信力造成极大损害，影响极其恶劣；文强肩负打击查处违法犯罪活动的重要职责，却在长期收受黑社会性质组织所送的财物后，不履行法定职责，包庇、纵容多个黑社会性质组织，致其得以发展壮大，长期实施违法犯罪活动，社会危害性极大，造成极其严重的社会影响，显然属于情节特别严重。本院依法对其判处死刑，剥夺政治权利终身，并处没收个人全部财产，罚当其罪。<br />
<br />
同庭宣判<br />
<br />
文强妻有期徒刑八<br />
<br />
犯受贿罪，因有自首情节，被减轻判处有期徒刑八年，并处没收个人财产人民币50万元。<br />
