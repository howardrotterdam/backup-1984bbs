庭审焦点 受贿1211万<br />
<br />
文强受贿的额度究竟是多少？<br />
<br />
经法院查明：1996年~2009年，文强多次单独或通过其妻周晓亚收受包括黑社会性质组织成员在内的他人财物，共计折合人民币1211万余元，其中，文强、周晓亚共同收受财物折合人民币449万余元。文强家财尚有1044万余元不能说明来源。<br />
<br />
2003年~2008年，文强明知岳宁、马当、王天伦、王小军、谢才萍等人组织、领导的多个黑社会性质组织从事有组织的违法犯罪活动，仍予以包庇、纵容。2007年9月，文强、黄代强明知王天伦犯罪组织因“涉黑”已被立案侦查，仍接受王天伦的请托，阻挠刑警总队对王天伦组织成员故意伤害致人死亡案件的查办，致使犯罪嫌疑人逃避打击。<br />
<br />
假画减贿364万<br />
<br />
昨日上午，短短14分钟的庭审，宣布了一直有所争议的文强、周晓亚收受赵利明一幅落款为张大千的“青绿山水”图(画名《蜀山携琴访友图》)的真伪，结论为一般仿品。因此，起诉指控文强受贿1625万余元，终被判定1211万余元。<br />
<br />
在文强案第一次庭审期间，公诉机关指控文强、周晓亚夫妇收受赵利明的落款为张大千“青绿山水”图，价值人民币364.12万元。对此，文强、赵利明及其辩护人均对该鉴定结论提出异议。休庭后，法院依法委托国家文物局国家文物鉴定委员会对该画进行了技术、质量鉴定，鉴定结论为：该画为一般仿品。<br />
<br />
当庭质证后，法庭认为，国家文物局下设的国家文物鉴定委员会对该画所作的鉴定结论，具有科学性、权威性，应予采信。故对指控该画价值364万余元的事实不予认定。<br />
