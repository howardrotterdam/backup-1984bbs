													<h2>文强表示要上诉 其姐欲卖房助其打官司</h2>
						
						
																											<div id="postmessage_563878" class="t_msgfont">中国新闻网4月15日报道 昨日下午，重庆五中院判定重庆市司法局原局长文强犯受贿罪，包庇、纵容黑社会性质组织罪，巨额财产来源不明罪，强奸罪等四项罪名，数罪并罚获死刑，剥夺政治权利终身，没收个人全部财产。文强昨日已表示要上诉。<br />
<br />
文强步履蹒跚到庭听判<br />
<br />
昨日上午，文强案在休庭两个多月后继续开庭审理。法院仅有30多人参加旁听。9时整，审判长发话：请法警带被告人文强、周晓亚、赵利明到庭。随即，文强在法警的搀扶下，步履蹒跚地走向被告席，精神大不如上次开庭。文强的穿着还是第一次庭审时的黑灰色休闲夹克衫。法院有关人士称，文强的腿部有些陈旧性的损伤，最近因为天气有点冷，气候有点潮湿，腿部有些隐隐作痛。<br />
<br />
上午的开庭只进行了14分钟，主要宣布文强、周晓亚夫妇收受赵利明的落款为张大千的“青绿山水”图(画名《蜀山携琴访友图》)，鉴定结论为一般仿品。<br />
<br />
犯罪所得2260万余元<br />
<br />
下午3时，审判长王立新开始宣读了长达160页的审判书，共宣读了1个半小时。法院判决称，1996年至2009年，文强利用其先后担任重庆市公安局党委委员、党委副书记、副局长，重庆市司法局党委书记、局长的职务便利，为他人职务晋升、调整，工作调动、安置，经营开发等谋取利益，文强单独或与其妻子周晓亚多次收受20多家单位和个人所送的财物1216万余元，其中，文强、周晓亚共同收受财物折合人民币449万余元。文强尚有1044万余元不能说明来源。<br />
<br />
2003年至2008年，文强明知谢才萍、岳宁、王小军、陈明亮、马当、王天伦为首的重庆五大黑帮从事有组织的违法犯罪活动，仍予以包庇、纵容，收受多个黑社会性质组织所送财物75.65万元。<br />
<br />
强奸女大学生罪名成立<br />
<br />
2007年8月28日晚，文强约女大学生巫某到重庆市渝北区一餐馆和一会所吃饭、唱歌，授意他人劝该女生大量饮酒后，将其带至一宾馆，不顾该女生反抗，强行与其发生了性关系。此外，文强对1044万余元财物不能说明来源。<br />
<br />
与文强一同收受巨额贿赂的其妻子周晓亚因犯受贿罪，具有自首情节，被从轻判处有期徒刑8年，并处没收个人财产人民币50万元。<br />
