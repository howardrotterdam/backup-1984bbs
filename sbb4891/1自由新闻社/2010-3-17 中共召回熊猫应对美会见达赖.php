													<h2>中共召回熊猫应对美会见达赖</h2>
						
						
																											<div id="postmessage_520343" class="t_msgfont">中国熊猫泰山在美受欢迎 奥巴马见达赖次日被召回<br />
2010年03月17日 14:49中国新闻网<br />
尽管很多人反对让熊猫变成中国图腾，但这种昂贵的国宝已经深度卷入了这个国家的经济、政治、文化和外交<br />
<br />
本刊记者/汤涌 文/李思佳<br />
<br />
熊猫“泰山”攀在树顶上远眺，看的是寂寞。他是一只ABP(America Born Panda)，出生在美国的熊猫。<br />
<br />
到3月9日为止，他必须自己独居一个月，这是他从美国华盛顿动物园归来的检疫隔离期。树顶能够让他看到其他不同年龄段的同类――这是之前他所没有见过的。<br />
<br />
由于卧龙基地在2008年的地震中受损正在重建，回国的泰山和其他一批卧龙熊猫生活在四川雅安的碧峰峡基地。<br />
<br />
“他在华盛顿吃四种竹子，我们给他提供了六七种。” 四川卧龙国家级自然保护区管理局副局长李德生说，“看他哪个季节爱吃哪几种的组合。”<br />
<br />
在华盛顿动物园里，4岁的他是唯一的明星，他和他的父母美香和添添，是华盛顿唯一的熊猫家族。美国人看着他出生，看着他长大，为他睡梦中打喷嚏的视频而惊呼和尖叫不已。<br />
<br />
泰山并非第一只出生在美国动物园的熊猫宝宝，但华盛顿动物园是国家动物园，地位特殊且门票免费。加上泰山出生在网络时代――他的童年就是在聚光灯下度过的。打开YouTube网站搜索Tai Shan，有超过4000个结果，也就是说，每天平均有3个以上关于泰山的视频被上传。访问量最多的视频超过了6位数，由于该网站无法在中国内地被访问，这些视频基本上可以反映国外熊猫迷的狂热程度。<br />
<br />
有一次，他的妈妈美香不慎把他推下了假山，在美香爬下假山把泰山抱住之前，美国的熊猫迷们已经把管理员办公室的电话打爆了。<br />
<br />
泰山出生在7月，每年的生日他都会收到饲养员精心制作的冰冻“蛋糕”，1岁那年是什锦水果冰蛋糕，4岁的时候是冰冻竹子蛋糕，大人们和孩子们看着这可爱的宝贝享受着他的生日礼物而欢呼不已。<br />
<br />
根据中美双方的协议，“泰山”本该在2007年7月9日满两岁后回归中国，但是两岁是一只熊猫最可爱的时候，看着“泰山”长大的美国人舍不得他回家，经过中方允许，于是“泰山”在美国又呆了两年。这使得小布什来北京参加奥运会开幕式时，泰山仍然在美国。<br />
<br />
2009年7月，泰山又一次延长留美半年，也许是一个巧合，这一次延长使得奥巴马访华时，泰山仍在美国。<br />
<br />
2010年1月，泰山被决定在农历新年前回国。和前两次一样，美国人尽力挽留，但泰山已经是一个4岁(相当于人类16岁)的大小伙子了。他需要一个年貌相当的配偶。<br />
<br />
对于中国来说，熊猫产生后代是头等大事，官方宣布的“回国繁殖”理由名正言顺，但仍有美国人揣测，中美之间的谷歌事件、对台售武等不愉快事件，也许是泰山没有多留半年的关键原因。泰山完全可以在半年后，和自己的交流期满的父母一起回家。<br />
<br />
2月5日，在白宫发言人宣布奥巴马将会见达赖的第二天，中国召回了熊猫泰山。<br />
<br />
熊猫见证的外交蜜月<br />
<br />
在日本的历史文献中，武则天在公元685年曾经送给平武天皇两只白熊和70张毛皮作为国礼。著名大熊猫研究专家胡锦矗曾经考证认为，这两只白熊就是熊猫。这也是中国历史上最早的熊猫外交。1941年，宋美龄曾经为了感谢美国帮助中国灾民而送给美国一对大熊猫，此外国外一些动物园和冒险家则通过民间收买、蒙混过海关的方式获得了中国的熊猫。<br />
<br />
但熊猫的珍贵被中国人认识到，还是近50多年的事――1955年，动物学家谭邦杰曾经在《生物学通报》上发表文章《大猫熊》，强调熊猫的珍贵:“它是极难得的动物(特别是对外国人)……它是我国最富有代表性的动物，所以以后一要使我国的人民熟识它。另一方面出口必须审慎。”那时谭邦杰工作的北京动物园也仅仅有三只刚从四川运来的大熊猫。<br />
<br />
谭邦杰仔细列举了熊猫和黑熊、棕熊等动物的头骨数据，得出的结论是:熊猫的头和嘴部比好几种熊都短，颧骨宽和嘴宽却要比熊大。换句话说，熊猫是一种大饼脸的动物。<br />
<br />
大饼脸、黑眼圈，也许是这种动物吸引人们的可爱之处。<br />
<br />
1957年5月，前苏联领导人伏罗希洛夫看到了北京动物园的大熊猫，非常喜欢，这位战场上的元帅毫不客气地开口索要熊猫，一要就是一对。此后朝鲜也前后要走了5只熊猫，但朝鲜的熊猫饲养水平不高，基本上要一只死一只。<br />
<br />
当时人工饲养熊猫繁育很难，这些熊猫都要靠四川、陕西深山里的猎户带着火把和猎犬，在悬崖密林中捕获。<br />
<br />
最著名的熊猫外交事件发生在1972年。美国总统尼克松访华并参观了北京动物园，和苏联人一样，尼克松也很希望得到中国的熊猫。据说当时周恩来总理拿着一盒熊猫牌香烟指给尼克松夫人:“我想送给你们这个。”尼克松夫人说自己不抽烟，周恩来解释说，礼物不是烟，是熊猫。当时尼克松夫人兴奋地大叫。<br />
<br />
于是，熊猫“玲玲”和“兴兴”去了美国，果然引发了美国人的第一波熊猫狂热。<br />
<br />
1972年9月，日本首相田中角荣访华，中国赠与大熊猫“兰兰”和“康康”，当载有这两只熊猫的专机飞入日本领空时，日本自卫队出动了两架战斗机护航，日本随即也掀起了熊猫热。<br />
<br />
1972年的动画片《熊猫家族》(原作者是著名画家宫崎骏)，主角小姑娘米米认了动物园的熊猫爸爸当爸爸，熊猫宝宝又认了米米当妈妈。和熊猫成为一家人，成了那一代日本孩子的梦想，那也是中日关系的蜜月期。<br />
<br />
自此，西方人和日本人提起中国时想到的不再是严肃的脸和灰蓝二色制服，而是黑白相间憨态可掬的熊猫。中国人也开始投其所好，服务外国人的友谊商店的标志变成了熊猫夫妇；1982年开始中国人民银行所铸造的国家金币也是熊猫图案。在20世纪80年代初期，熊猫被人们尊为“国宝”。1983年以来，四川遭遇大面积的竹子开花，尽管现在有很多专家认为竹子开花对熊猫生存的威胁没有那么大，但当时确实引发了全国人民为熊猫募捐的狂潮。<br />
<br />
熊猫逐渐成为西方寓意中国的动物，这种动物代表了西方的一种期许:有力量、很少咬人、不争肉吃，比美国的鹰和俄罗斯的熊都要温和。在希望表现中国威胁的时候，西方漫画家依然会画更有攻击性的龙<br />
一对熊猫的价格<br />
<br />
1958年，北京动物园曾经和奥地利商人进行过动物交换，一只雌性熊猫换来了3只长颈鹿2只犀牛，还有河马和斑马。这种动物交换仅此一次。而在20世纪80年代，熊猫则走上了出国打工的道路。1982年，中国宣布不再向国外赠送大熊猫。<br />
<br />
“当时的普遍做法是短期借展，一般是3个月左右。”中国动物园协会高级工程师刘农林回忆说，“1984年洛杉矶奥运会的时候，中国就租借了一对熊猫给洛杉矶。”<br />
<br />
这种“熊猫打工仔”模式遭到了国际上的批评，尤其是一只熊猫租借出去巡展时发情了，它身边没有异性同类。让如此濒危的动物错过了一次交配机会，引起了诸多批评声。<br />
<br />
这种短期巡展的租金是10万美元/月/对，这在20世纪80年代是相当可观的数字，当时有很多国内的动物园和繁育机构试图从中获利，而在野外捕捉熊猫。北京大学自然保护与社会发展研究中心的王大军博士评价说:“对于野外熊猫来说，这种捕捉和偷猎没有区别。”<br />
<br />
在许多美国公民的游说下，美国联邦渔业和野生动物管理局禁止了商业目的的熊猫租借，中国之后也宣布停止商业目的的熊猫出租。<br />
<br />
取代短期租借的是“合作研究协议”，中国把熊猫借给外国动物园供研究用，为期十年；外国动物园则支付一笔给熊猫繁殖保护事业的专项款，同时提供一些额外的资金用于熊猫的野外保护。<br />
<br />
美国人做了一个简单的乘法，这使得他们在后来叫苦不迭。一对熊猫每年就得支付100万美元的科学研究用款，如果熊猫宝宝诞生，还要多支付60万美元。曾经有美国动物园私下表示过，能不能让生下来的熊猫归自己，但中国一直没有同意。<br />
<br />
中国也曾经和泰国签署过类似的协议，泰国的动物园每年支付28万美元。这个价钱和美国相比可谓相当便宜，但泰国的大象饲养员还是觉得政府在熊猫身上花费太多，他们甚至把大象涂成黑白色的熊猫装扮，上街游行以呼吁政府关心本土动物亚洲象。<br />
<br />
“现在美国几家私营动物园觉得熊猫太贵，”刘农林说，“十年的时间，人们对熊猫总会有审美疲劳的时候。”<br />
<br />
“熊猫打工”挣来的这笔资金对于建设熊猫繁育基地却起了至关重要的作用。20世纪80年代末，中国动物园协会在和美国、日本谈熊猫“合作研究”，美国人正在忙着算收支账的时候，日本人已经决定付钱了。<br />
<br />
“当时的成都繁育基地只有一块地，钱要自己找，日本人先拿出了一年的钱，才能有今天的成都基地。”刘农林说。<br />
<br />
盗猎一只熊猫至少将给国家造成50万美元的损失，于是最高人民法院1987年7月14日发布通知:对猎杀大熊猫并出卖大熊猫皮的，应依照全国人大常委会《关于严惩严重破坏经济的罪犯的决定》从重判处。最高刑是死刑。这一决定现在已经失效，但在当时确实有盗猎者因为一张熊猫皮被判死刑。<br />
<br />
哪个系统的熊猫<br />
<br />
中国圈养的熊猫，分属于两个系统。一只熊猫从生下来开始，就有着自己的烙印――建设部的熊猫，或者国家林业局的熊猫。比如亚运会吉祥物盼盼(原名巴斯，会耍杂技，后来训练熊猫耍杂技被国家禁止了)就是林业局的熊猫，而奥运会福娃晶晶的原型，则是建设部的熊猫。<br />
<br />
在1949年之后，动物园被当做“养动物的公园”来对待，属于建设部以及各地建委管辖的园林局系统，而国家林业局则承担野生动物保护职责。两个部委下属的协会中国动物园协会和中国野生动物保护协会都曾送过熊猫出国。<br />
<br />
“我们有好几年没有成功地送熊猫出国了，”中国动物园协会高级工程师刘农林说，“这次回来的泰山是林业部(国家林业局旧称)的熊猫。近几年我们递交的送熊猫出国的方案，总是批不下来。”<br />
<br />
送熊猫出国，在过去需要建设部、林业部和外交部三部会签。但在最近几年，“不知道为什么变成了两部会签。”刘农林说。<br />
<br />
两个系统各自有自己的熊猫种群，国家林业局的基地是卧龙基地(地震后熊猫迁往四川雅安碧峰峡基地)，有熊猫61只，而建设部的基地则是成都大熊猫基地，有熊猫83只。这两个基地相隔不远，但由于分属不同的系统，无法联姻而只能内部繁殖。<br />
<br />
“最极端的一件事，是上海动物园曾经向林业系统的某单位租一对熊猫。为了防止产生的幼崽被动物园留下，那个单位给了动物园两只公熊猫，这是很不人道的。”刘农林说，“老部委有许多利益纠葛，最好能把两个系统的动物保护工作都抽离出来，交给比如环保部这样的年轻部委来统筹，能大大提高工作效率。”<br />
<br />
“最近的情况好多了，”四川卧龙国家级自然保护区管理局副局长李德生说，“随着一些地方的园林局和林业局的合并，现在基地和各地动物园的交流越来越多。”<br />
<br />
尽管地震对熊猫种群造成了损失，但近几年养育熊猫技术的突飞猛进，成都和卧龙的熊猫宝宝几乎以一年十几只的速度在增长，但熊猫的命运仍然无法让中国的熊猫保护者松一口气，尤其是野外熊猫的命运。<br />
<br />
泰山出生后第一次与游人见面时，王大军博士正好在华盛顿。作为合作伙伴，华盛顿动物园的园长兴奋地邀请王大军去看泰山，那口气就像是一个老来得子的父亲。“你报我的名就可以进去，不用票！”<br />
<br />
华盛顿动物园门票和熊猫馆门票都是免费预约，但是由于想看泰山的人太多，华盛顿动物园门口出现了黄牛党。<br />
<br />
王大军终于没有去看泰山，他觉得隔着玻璃窗看熊猫的感觉不好:“人工饲养的熊猫固然可爱，但当你接触野外熊猫多年，闻它的气味，它也能信赖你，你可以和它像朋友一样坐在一块石头上呆半天。经历过这些事，我对人工养的熊猫就兴趣不大了。”<br />
<br />
4岁的泰山即将迎来的是他的人生大事――繁殖，在野外，只有七八岁的壮年熊猫才能赢得竞争和获得配偶，但圈养的熊猫必须适当早婚早育――他们的孩子对于这个国家和熊猫家族来说都非常宝贵。<br />
<br />
如果接下来的几年里，泰山无法成功地让母熊猫怀上自己的宝宝，他就会从明星熊猫变成很次要的熊猫。不适合繁殖的熊猫将走上另一条路――也许要背井离乡到二三线城市的动物园和那里的小朋友见面，直到终老。<br />
<br />
尽管曾经有25岁的老年熊猫在深圳野生动物园与游人合影创收的负面消息传出，和其他动物相比，熊猫们的生活可谓相当人道:正在重建的卧龙大熊猫中心园区里将会增加一个熊猫养老院。在那里，白内障、断肢和患有各种老年病的熊猫将会在故乡走完生命的最后里程。 ★ <br />
<br />
(本文来源:中国新闻网)</div>

							
							
							
							
