													<h2>绿坝软件北京项目组遭遣散 2000万用户被弃</h2>
						
						
																											<div id="postmessage_736544" class="t_msgfont">位于北三环华杰大厦的绿坝项目办公室大门紧锁，屋内空空荡荡。 本报记者胡雪柏摄<br />
<br />
京华时报7月13日报道 去年炒得沸沸扬扬的绿坝软件沉寂一年之后，曝出项目组遣散的消息。<br />
<br />
记者近日调查发现，去年由工信部推广的绿坝・花季护航软件北京项目组因经费问题已经关张，所有人员遣散;而位于郑州的另一项目组也在艰难维持，随时可能关张，届时将有2000万绿坝用户失去技术和客服支持。<br />
<br />
&gt;&gt;现状<br />
<br />
绿坝北京项目组已解散<br />
<br />
上周五，一位神秘人士报料称，绿坝・花季护航软件项目组位于北京华杰大厦的项目办公室已经关张，所有员工都被遣散，希望记者关注。当天，记者来到北京华杰大厦B座411室。当时正值工作时间，该办公室大门紧锁，里面空空荡荡，没有桌椅和办公设备。<br />
<br />
绿坝项目是由北京大正语言知识处理研究院和郑州金惠计算机系统工程有限公司共同承担。北京大正总经理陈小盟承认，位于华杰大厦的项目办公室确实已经于上月底关闭，将近30多名员工已经遣散。<br />
<br />
他们还有一些人来自郑州，都陆续离开公司。“但是他们不可能回北京大正办公，因为他们是工信部成立的绿坝项目的工作人员，我们不能接收。”<br />
<br />
陈小盟透露，绿坝・花季护航项目成立于2008年5月，由郑州金惠的“金惠堵截黄色图像及不良信息专家系统”和北京大正的“花季护航上网管理软件”两款产品中标，后来将这两款产品组合，并更名为绿坝・花季护航。工信部采购了4170万元人民币的该软件，提供给网民免费下载。<br />
<br />
&gt;&gt;探因<br />
<br />
国家一年多未拨付经费无力再支撑<br />
<br />
“我们真的缺乏经费支持，已经苦苦撑了一年了，再也熬不下去了。”陈小盟昨天说，去年政府总共出资4170万元采购该软件，北京大正拿到1990万元，郑州金惠分得2180万元，但这只是2008年到2009年一年的费用，从去年至今两家公司都没有得到一分钱的运营费用。<br />
<br />
据悉，工信部在去年4月要求安装的这款上网过滤软件，名为绿坝・花季护航，由绿坝和花季护航两款产品集合而成。工信部等几部委斥资4170万元采购，买断其一年服务，供全社会免费使用。<br />
<br />
陈小盟告诉记者，该北京项目办公室一年需要700多万元的运营成本。他从去年开始就给有关部门写报告反映情况，但一直未得到任何回复。陈小盟说，北京大正是个以研发信息安全、搜索、机器翻译等技术为主的企业，产品的市场化程度不高，企业成立10年来只能做到盈亏平衡，根本没有财力支撑绿坝这个项目。<br />
<br />
他透露，在关门前，绿坝的北京办公室其实已经搬过一次家，当时办公室在富海大厦，但是租金一年60多万，由于新的运营费用迟迟未给，只好搬到租金便宜的华杰大厦，省了20万左右。“现在实在撑不下去了，很多员工都主动辞职了，最后我们不得不直接关了。”<br />
<br />
昨天，记者联系工信部了解有关绿坝项目的情况。不过，截至发稿前，未得到任何回复。<br />
<br />
记者昨天拨打郑州金惠绿坝项目的客服电话，对方称郑州绿坝项目目前运转正常。郑州金惠一位员工透露，绿坝项目的国家经费已经拖了一年没拿到了，再这样下去，该项目也很难支撑多久了。<br />
<br />
&gt;&gt;影响<br />
<br />
2000万用户将无技术支持<br />
<br />
目前，绿坝・花季护航的用户已经突破2000万，大部分是学生家长、学校和网吧。如果北京和郑州两个项目组都停了，对2000万用户会有何影响?<br />
<br />
对此，北京大正总经理陈小盟表示，该项目由北京和郑州两地共同维护，其中北京的绿坝项目有将近30人，都是北京大正临时招聘而来，主要负责绿坝・花季护航官方网站的维护和软件推广。位于郑州的绿坝办公室主要由郑州金惠公司承担，负责该软件的技术、售后、客服等任务。他们负责的该软件的官方网站目前已经处于无人维护和更新的境地，长此以往，用户就无法从网站免费下载软件，软件的推广工作也会搁置。<br />
<br />
上述郑州金惠员工表示，北京的项目组关停影响最大的是软件下载，但是郑州项目组如果停了，大量用户将无法获得软件技术支持，日常的客服也将中断，势必会给大量用户造成困扰。<br />
