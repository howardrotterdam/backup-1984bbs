前西藏政府噶伦（部长）、后出任中共全国人大副委员长的百岁老人阿沛.阿旺晋美年前在北京去世，中共举行了隆重悼念仪式，歌颂他是&quot;伟大的爱国主义者，中华民族的功臣&quot;；与此同时，在印度的西藏流亡政府也发表了悼念公告，赞誉他是&quot;一位维护民族尊严的爱国者&quot;。<br />
<br />
&nbsp; &nbsp;北京和达兰萨拉同时肯定一个藏人是&quot;爱国者&quot;，这是罕见的。但这里的区别，是双方对&quot;国&quot;的认知不同。对北京来说，五十年代初阿旺晋美代表西藏政府到北京跟中共签了《十七条协议》，&quot;使西藏回到祖国怀抱&quot;，当然是红色中国的&quot;功臣&quot;。但对西藏流亡政府来说，阿旺晋美虽做了北京高官，但仍心向西藏，&quot;在极为艰困的环境下仍极力设法传达&quot;有利西藏民族的&quot;真实信息&quot;。而且在跟中共签《十七条》时，也曾为西藏利益据理力争。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;在1997年10月的伦敦&quot;汉藏对话会&quot;上，我曾采访到当年随阿沛.阿旺晋美到北京跟中共谈判的藏人翻译达克拉.彭措扎西，他的回忆也证实了达兰萨拉的说法。彭措扎西不是普通的翻译，三十年代曾在蒋介石任校长的南京中央政治学校读书，说一口流利的汉语；后来担任达赖喇嘛的警卫团长，以及西藏流亡政府的安全部长。彭措扎西的夫人达克拉.格桑央吉,曾任达赖喇嘛驻英国代表，也做过驻台湾的首任代表，现任西藏流亡政府外交与新闻部部长。除了在伦敦那次采访，后来有一次在去新德里的飞机上，我又跟他们夫妇巧遇。虽然当时彭措扎西已七十多岁，但反应机敏，记忆力惊人，对当年去北京谈判的细节如数家珍。他说，抵达北京后，阿沛.阿旺晋美就提出四条：一，承认北京新政府；二、解放军占领的昌都以东土地还给西藏；三、新政府驻藏人员不能超过百人；四、解放军不进藏，国防由西藏政府自己解决。阿旺晋美明显想为西藏争利益，试图保持以往西藏跟蒋介石国民政府的那种&quot;关系&quot;：对外，可宣称西藏从属中国，对内，藏人完全自己管理。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;但中共不接受，拿出已拟好的《十七条》要西藏代表签字盖章，但遭阿旺晋美等拒绝。彭措扎西说，当时中共首席代表李维汉拍桌子喊，不签，解放军就打进拉萨，&quot; 你们自己选择，是武力还是和平！&quot;但阿旺晋美们仍是拒绝，这样来回冲突、谈判了几个月，最后迫于无奈，藏人代表才在十七条上签字。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;为了西藏利益，阿旺晋美还想出三个对策：第一，事先不向拉萨请示，为西藏政府一旦不接受这个协议留下余地；二是虽然他身带昌都总管的官印，但佯称没有。最后藏人代表的印章，是中共方面刻制的；三是坚持在十七条之外再签个附件，七个条款中有一条：达赖喇嘛一旦不同意这个协议，逃到别的国家，任何时候回来，其政教领袖地位都不变等。但中共没有公布这个附件。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;阿沛.阿旺晋美等西藏代表最后所以在十七条上签字，主要担心一旦解放军打进拉萨，藏人损失太大，视协议为缓兵之计。昌都一战，阿旺晋美的几千装备落后的藏军，被中共军队完全击败。刚打赢国民党的解放军，军队人数超过了当时整个西藏人口，这个仗没法打。昌都战败当了俘虏的阿旺晋美对此更有亲身体会，所以才会在协议上签字。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;达赖喇嘛后来说，他是从收音机听到《十七条》内容的。从这也可看出，十七条是中共大军压境下的威逼产物，根本不是公平协议。但即使这样，西藏政府最后还是接受了，毕竟双方力量太悬殊。另外，藏人那个时候怎么能想象到，共产党是那样残暴。不要说西藏人，连跟共产党一直交手，誓言&quot;攘外必先安内&quot;、剿灭共匪的蒋委员长，西安事变后，居然向共军提供粮饷，将其正式编入国民政府军，结果养虎为患，自己被吃掉。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;阿沛.阿旺晋美后来当了中共人大副委员长，成为北京拉拢藏人、殖民统治西藏的傀儡。但从阿旺晋美的几个举动，可看出他仍心向西藏。<br />
&nbsp; &nbsp;<br />
<font color="red"><br />
&nbsp; &nbsp;1989年初，十世班禅在拉萨圆寂，按惯例，寻找&quot;转世灵童&quot;。北京对此相当积极，因中共一直用三十年代国民政府和西藏的交往，来证明西藏从属中国；强调蒋政府特使吴忠信去拉萨查看了转世灵童，最后决定免于抽签，而且主持了第14世达赖喇嘛&quot;坐床仪式&quot;（即登基典礼）等。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;但阿沛.阿旺晋美却对此公开提出不同看法。1989年夏，阿旺晋美在西藏自治区人大会议讲话时，特意提到这段历史，说他曾通过江苏省委和省政府，在南京档案馆查看了当年西藏政府和南京政府就这个问题的往来电报信件等，弄清了几个关键问题：一是灵童只有一个，因而不存在&quot;抽签&quot;问题；二是灵童已在青海选定，不存在中央特使&quot;查看、确定&quot;问题；三是西藏政府所以给南京政府写信，是因为青海军阀马步芳勒索银元300万，不让灵童离开，藏人拿不出，只好给南京写信，希望蒋政府能斡旋，让灵童去拉萨。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;当时国民党《中央日报》曾以套红报道说，吴忠信&quot;主持了&quot;达赖喇嘛坐床典礼，并配发了一张吴与达赖喇嘛在一起的照片。阿旺晋美说，他仔细看发现，这不是坐床仪式的照片，而是在达赖喇嘛的卧室拍的。阿旺晋美在讲话中明确地说，&quot;实际情况是不存在吴忠信为 14世达赖喇嘛主持坐床典礼的事实。&quot;他还强调，&quot;今天我们在座的有许多原来西藏的老贵族，都知道达赖喇嘛坐床典礼的习惯和它的程序是没有什么主持人的。不像汉族开会时有个主持人。&quot;他借此机会表示：&quot;在这个问题上，国民党说瞎话，我们就没有必要继续跟着他们说瞎话。&quot;<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;阿沛・阿旺晋美的这番讲话，发表在次日（1989年8月27日）中共《拉萨日报》第一版，等于公开澄清了这段史实，驳斥了国共两党的谎言。</font><br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;阿旺晋美还曾提出&quot;所有藏民族地^F统一&quot;，即恢复&quot;所有藏^的整w性&quot;，以保留西藏文化历史和宗教。1991年，阿旺晋美还公开强调，藏族跟其他少数民族不一样，西藏跟中央政府签有&quot;十七条&quot;，呼吁中国政府落实协议中的西藏&quot;政治宗教地位不予更&quot;的条款，强调藏人治藏。这跟达赖喇嘛主张的西藏&quot;高度自治&quot;很相像。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;长寿百岁的阿沛.阿旺晋美，去世前几年，身体非常衰弱。他在美国的一个儿子对我说，他回去探亲，父亲神智不清，不能言语，没法交流了。但在2009年3月拉萨事件时，中共却发表了阿沛.阿旺晋美谴责藏人暴力的所谓&quot;谈话&quot;，显然是编造的。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;阿沛.阿旺晋美希望通过跟中共妥协，保存西藏，结果事与愿违，西藏变成了北京的殖民地，西藏作为一个有自己独特文化和宗教历史的种族，正在一步步被灭绝。阿旺晋美本人则是一个寄人篱下、任北京摆布的屈辱象征。阿沛.阿旺晋美的一生，是一个悲剧，也是西藏灾难的缩影。但悲哀的是，他的去世，并没有带走这个屈辱的时代；半个世纪过去了，西藏人民的奋斗，恐怕还要从十七条之前起步。</div>

							
							
							
							
