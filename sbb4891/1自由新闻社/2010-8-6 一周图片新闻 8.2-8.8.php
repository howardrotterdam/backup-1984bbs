 <br />
　　8月2日，朝鲜大型团体操和文艺表演《阿里郎》在平壤五・一体育场举行今年的首场公演。《阿里郎》由朝鲜10万名青少年和专业艺术工作者参与演出，刻画朝鲜近现代重大历史事件。《阿里郎》自2002年首次公演以来，已成为朝鲜文艺表演历史上观看人数最多的演出。 <br />
