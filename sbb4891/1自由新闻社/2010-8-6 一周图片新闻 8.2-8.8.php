&nbsp; &nbsp;&nbsp;&nbsp;阿里郎节得名自朝鲜传统情歌《阿里郎》。在这个节日中，上千名团体操演员会表演整齐划一、场面宏大的歌舞，孩子们通过变换手中的彩色纸板组合出壮观的图案。图中的背景标语义为：我们的祖国繁荣昌盛。 <br />
