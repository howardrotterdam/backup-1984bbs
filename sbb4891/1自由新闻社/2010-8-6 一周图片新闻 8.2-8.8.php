 <br />
　　8月2日，朝鲜大型团体操和文艺表演《阿里郎》在平壤五・一体育场举行今年的首场公演。分析认为，强调中朝友谊是由于朝鲜最高领导人金正日5月访问了中国、朝方正在加大吸引中国游客的力度。今年的演出将每周上演4次至10月中旬。上海至平壤的包机航班将于这一期间开通。 <br />
