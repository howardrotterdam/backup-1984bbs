 <br />
　　8月2日，朝鲜大型团体操和文艺表演《阿里郎》在平壤五・一体育场举行今年的首场公演。据日本共同社报道，为突出与中国的友好关系，今年的演出中新加入了主题为“友好阿里郎”的内容。表演者排列组成了“朝中友谊与鸭绿江江水永存”、“朝中友谊源远流长”等口号，还有表演者扮成熊猫或身着中国民族服装敲起了腰鼓。 <br />
