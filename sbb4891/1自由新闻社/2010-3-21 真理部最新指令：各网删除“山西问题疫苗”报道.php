<br />
报道的调查中还指称这个机构的一名高级管理人员，通过向他任要职的一家公司购买生物制品，参与腐败活动。<br />
<br />
*国际记者联盟批中国当局禁言令*<br />
<br />
国际记者联盟秘书长艾丹.怀特说，媒体在公共健康问题和政府部门受控渎职的信息方面可以保证公众的知情权，而禁止报道这一严重的公共健康议题是不负责任的，政府必须为此承担责任。<br />
<br />
国际记者联盟再次呼吁中国中央政府和地方政府取缔限制报道的做法，尊重中国总理温家宝有关关鼓励进一步开放媒体的政策。<br />
<br />
