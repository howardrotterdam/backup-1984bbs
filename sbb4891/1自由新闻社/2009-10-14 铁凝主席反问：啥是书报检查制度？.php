																											<div id="postmessage_282405" class="t_msgfont"><strong>一生仰慕中国的布莱希特这么说;“最糟糕的不是犯了错误却不思悔改，而是犯了错误却试图掩盖。”</strong><br />
<br />
在北京凯宾斯基酒店大堂的沙发上端坐着一位穿着优雅黑衬衫的与众不同的女士，她叫铁凝，是中国作协主席；8920位拿国家俸禄的男女作家的头头。<br />
“书报检查制度？啥叫书报检查制度”，她反问到。“在中国艺术家享有极大的自由。”并且&quot;我们热烈期待将在法兰克福进行的开放的思想交流。”<br />
<br />
这将成为一次欢快的书籍盛会。作为特邀嘉宾，这个星期中国官方将组团参加全球最大的书展，团员包括为数整整100人的作家，以及另外1000位各级主管官员和出版社经理。<br />
法兰克福的组织者承诺，这将会是一次“批评性的对话”。<br />
<br />
严肃的铁女士在北京说；“人们当然必须遵守一定的法律法规，比如不能侮辱少数民族。仅此而已。”<br />
她当然从未听说在中国年复一年，每年都会有600本书被禁。然后她挺直了腰板，插好胸针，堆出了一副混凝土式的微笑。<br />
<br />
<br />
<strong>“你给我看一滴泪，我看见了你心中的海洋。”</strong><br />
<br />
记者无国界的全球思想和新闻媒体自由度排行榜上，中华人民共和国在173个国家中排名167.<br />
大赦国际说至少40位记者和作家身陷囹圄。酷刑和虐待到处泛滥。环保分子遭到国安人员的跟踪。<br />
有例子说明，只消一个抗议藏人和维人的人权遭侵犯的条幅，就可能被判数年徒刑。<br />
在经济奇迹发生之地的成百万台的电脑上，因为网页被禁，可能好多天都无法浏览facebook。<br />
这一切众所周知，令人悲哀。<br />
<br />
可人们也常常遇到令人惊讶的反西方言论。很多中国人并不象西方人那样觉得国家的压迫有多么糟糕。<br />
<br />
在北京三环边上的一个代理处人们能碰到一位瘦瘦的年轻小伙子，这里他被当成著名的忧郁诗人，<br />
青年中国文学的明星。 与当代中国事实上的那样相反，在他身上绝不会让人有一丝一毫受到压抑的印象。<br />
<br />
郭敬明今年26岁，但瘦得和十几岁似的。头发向前梳，经过精心的梳理。他脸颊上抹着胭脂，身上穿着毛衣，脚上蹬着白色的网球鞋。<br />
他说，他18岁就开始写作了，并且只写真正感动过他的东西。也就是他的生活和爱情。比如像这样“你给我看一滴泪，我看见了你心中的海洋。”（原文求教达人）<br />
他最新的书叫“小时代”。<br />
。。。<br />
<br />
<strong>“世上最难的进步莫过于回到理智”</strong><br />
<br />
也许是这个国家太大而无法控制，也许一切都是偶然的巧合：余华如今名利双收，而且能在法兰克福代表整个中国文学的成就，而其他作家或者坐牢，或者写一些在中国永远不能出版的书。让人无法理解的是，普通的抗议和持不同政见的分界究竟在哪里。<br />
<br />
即便是作家阎连科其实也不是持不同政见者。尽管如此他还是写了一部相当大胆的书。这部书叫”我祖父的梦“，它记述了一件真实的丑闻：几年前由于针头和血液制品遭到污染，数万人感染了HIV（艾滋病）。然而那些肇事的血贩子却受到腐败党政干部的包庇。这本书在中国自然是被禁了。<br />
<br />
阎连科直到90年代中期都是职业军人，作为一名文职军官，他住在一个部队驻扎区。后来因为一些不和谐的文章和访谈，他不得不脱去了军装。不久前他成了北京人民大学的文学教授，在那里教授二十世纪经典文学。<br />
<br />
”我祖父的梦“并非阎连科的第一本禁书。在早先一部2005年的小说里描写了一位高级军官的妻子和他的下属因为偷情时房事过于激烈，打碎了毛的胸像：书报检查官觉得玩笑开大了。<br />
<br />
。。。<br />
