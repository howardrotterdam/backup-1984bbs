													<h2>太皇太后晚年借钱捐款</h2>
						
						
																											<div id="postmessage_183873" class="t_msgfont"><a href="http://news.qq.com/a/20090730/000131.htm" target="_blank">http://news.qq.com/a/20090730/000131.htm</a><br />
卓琳在家中度过最后时光 晚年借钱向震区捐款<br />
 2009年07月30日03:40 　 新京报　 <br />
本报讯（记者徐春柳）中国共产党的优秀党员，久经考验的忠诚的共产主义战士，中央军委办公厅原顾问卓琳同志因病医治无效，于2009年7月29日12时30分在北京逝世，享年93岁。 <br />
从昨天下午开始，景山后街米粮库胡同的邓家已在操办后事。据门前的警卫称，里面在设灵堂，今天将接受国家领导人与至亲好友的吊唁，但暂不会开放公众去家中的灵堂。<br />
<br />
　　卓琳1916年4月6日出生于云南省宣威县，1937年参加革命工作，1938年4月加入中国共产党，1943年9月任中共中央北方局秘书处新闻材料室组长，1950年3月任中共中央西南局人民小学校长，1952年任中共中央秘书处机要秘书。“文化大革命”中，邓小平受到错误批判和斗争，她也受到牵连，1969年10月随邓小平到江西，在新建县拖拉机修造厂劳动。1973年任国务院办公厅机要秘书，1978年1月任中央军委办公厅顾问，1988年被授予中国人民解放军独立功勋荣誉章。<br />
<br />
　　现场<br />
<br />
　　卓琳在家中　度过最后时光<br />
<br />
　　胡同街坊自发到邓家门前驻足哀悼<br />
<br />
　　在北京景山后街的米粮库胡同里，有一个两进的普通院子，灰砖灰瓦，方方正正，院内种满了花、草、树，郁郁葱葱。邓小平全家于1977年搬到这里。<br />
<br />
　　昨晚，这个大院的大门紧闭，从门外望进去，只见被世人所熟知的两棵松树在黑夜中相互依偎。时有邻居经过，在门前驻足。胡同内的张大妈称，卓琳老人已在此居住20余年。昨日下午，悉知卓琳老人去世，米粮库胡同的街坊们都自发到大院门前驻足哀悼。<br />
<br />
　　大院旁家属院的宇文华老人表示，昨日下午16时许，米粮库胡同内开进多辆车，其中有两辆面包车装满鲜花，还有松柏，另外一辆车装满黑布、白布等。“来了很多警察，还有当兵的，今晚要连夜做花圈布置灵堂”。<br />
<br />
　　这个大院居于米粮胡同中间，左边是卓琳老人居所，右边是作为警卫的第七中队。大院中有电工、厨师、清洁员、司机等，负责照顾邓小平家人的生活。宇文老人称，她的女婿便是卓琳的司机。据她了解，今年春节后，卓琳老人病重，遂入院治疗。前几日，卓琳老人随家人回到米粮库胡同的家中，度过最后时光。<br />
<br />
　　本报记者刘泽宁实习生李莎莎<br />
<br />
　　人物<br />
<br />
　　卓琳，小平半个世纪的“拐杖”<br />
<br />
　　“她始终都认为邓小平是对的，包括在‘文革’时，邓小平被打倒。”中央文献研究室邓小平研究组副主任刘金田因工作关系，与邓家多有接触。他眼中的卓琳，对丈夫坚定支持，对亲友和蔼可亲，对孩子们严格要求。<br />
<br />
　　青年云南读北大第一人<br />
<br />
　　卓琳出身名门，本名浦琼英，云南宣威人。父亲是名实业家，经营著名的宣威火腿，曾是追随孙中山革命的北伐军少将。<br />
<br />
　　刘金田介绍，卓琳是云南省第一个考上北大的学生，另外她还是云南省百米跑运动员。在北平，她一边学习，一边参加抗日救亡进步活动。七七事变后，她同两个姐姐来到延安，并在这里结识了邓小平。<br />
<br />
　　“他从前方回来，经常到公安部里来玩，有时我们也到他们驻地去玩。他大概那个时候对我有意了，但我不知道，他就找跟我一块到延安的女朋友跟我谈，说他想跟我结婚……后来，他两次主动找上门。听他讲完有关情况和理想后，觉得他也是个知识分子。我想，反正早晚都得结婚……”多年后，卓琳平实而又深情的回忆，记述了战争岁月那段浪漫的爱情。<br />
<br />
　　婚后跟着小平起起落落<br />
<br />
　　结婚以后，卓琳一直从事教育工作。据刘金田介绍，自邓小平到中央以后，卓琳一直在小平办公室当秘书，但她从不参与其他事，只管收文件、发文件这类的事情。<br />
<br />
　　“文革”开始后不久，邓小平被打倒，他和卓琳都被软禁起来。在经过无休止的批斗之后，她被惩罚打扫卫生。“卓琳和邓小平一直到最后去世，都是相互依靠，都是非常好的。”刘金田说。<br />
<br />
　　晚年借钱向震区捐款<br />
<br />
　　卓琳的晚年生活，最多的是听戏，还喜欢听评书。她曾请人帮忙找了很多诸如《三国演义》、《隋唐演义》等评书的录音带，没事的时候就听上几段。<br />
<br />
　　<strong>卓琳老人的最后一次公开报道，是在去年汶川大地震时：中国红十字会证实，5月13日和5月14日，卓琳的家人连续两天匿名捐款后，5月15日，卓琳老人又拿出自己积蓄多年、不足10万元的工资，准备全部捐出。老人还特地向家人筹措了部分资金，凑足10万元的整数，而所借部分将从她日后的工资里扣除。</strong><br />
<br />
　　本报记者徐春柳<br />
