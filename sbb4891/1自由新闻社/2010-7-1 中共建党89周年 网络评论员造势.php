　　我们党是一个执政61年的大党,保持党的生机和活力始终是关乎党和国家前途命运的重大问题。当前我国发展的外部环境和内部条件发生了很大变化,加快转变经济发展方式的重要性和紧迫性更加凸显,全面建成惠及十几亿人口的更高水平的小康社会任务光荣而艰巨,维护社会和谐稳定、保障改善民生、解决人民群众最关心最现实最直接的切身利益问题需要付出更多努力。面对世情、国情、党情的深刻变化,面对严峻复杂考验,党内还存在一些不适应新形势新任务要求、不符合党的性质和宗旨的问题。全党同志惟有不断增强忧患意识、危机意识、使命意识和责任意识,认真清扫政治灰尘,加强党性修养和作风养成,脚踏实地干事创业,尽心竭力造福于民,我们党才能始终走在时代前列,引领中国现代化航船劈波斩浪不断前行。<br />
<br />
　　我们党是一个走过89年光辉历程的大党,党的建设始终是我们党开辟前进道路的强大动力。在革命、建设、改革的各个历史时期,党的基层组织和广大党员充分发挥了战斗堡垒作用和先锋模范作用,为党和人民建立了巨大功绩。在新的时代条件下,党的基层组织和广大党员要把忧党之心转化为兴党之责,着力推动科学发展,认真落实科学发展规划、思路和举措,以模范行动影响和带领广大群众努力完成各项工作任务,为加快经济发展方式转变、保持经济平稳较快发展贡献智慧力量;着力促进社会和谐,掌握社情民意,协调各方利益,拓宽诉求渠道,化解矛盾纠纷;着力服务人民群众,深入基层,深入实际,深入群众,及时回应群众关切和期待,积极解决群众反映强烈的突出问题,多为群众办实事办好事;着力加强基层组织,扩大组织覆盖,创新活动方式,积极推进学习型党组织建设,增强党员队伍的生机活力,通过基层党组织建设带动其他各类基层组织建设。深入开展创先争优活动,重在解决问题,重在取得实效。只要各级党组织、广大党员干部特别是领导干部履职尽责、率先垂范,我们就能够保持党的先进性,推动党和人民事业不断向前发展。<br />
<br />
　　明年,我们将迎来建党90周年。回首近一个世纪的光辉历程,党的建设始终同中国现代化事业紧密联系在一起,同中华民族伟大复兴紧密联系在一起, 同人民群众根本利益紧密联系在一起。我们所处的时代充满机遇和挑战,我们肩负的使命伟大而光荣。全党同志常怀忧党之心,恪尽兴党之责,勇于改革创新,争当时代先锋,就必定能够创造无愧于中华民族的出色业绩。<br />
