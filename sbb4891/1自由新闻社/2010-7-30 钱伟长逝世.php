　　钱伟长简历<br />
<br />
　　男,汉族,1912年10月生,江苏无锡人,民盟成员,加拿大多伦多伦多大学博士毕业照多大学应用数学系毕业,博士,研究员、教授,中国科学院院士。钱伟长,中科院院士,我国著名物理学家,与钱学森、钱三强一起并称中国科学界“三钱”。<br />
