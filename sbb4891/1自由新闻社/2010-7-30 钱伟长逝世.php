<br />
　　钱伟长,1912年10月9日出生于江苏省无锡县鸿声乡七房桥村。20世纪初,中国正处于推翻帝制,建立共和,西学东渐,国学衰微之际。钱伟长就出生在一个具有国学功底、创办新学的贫穷的诗书家庭……<br />
<br />
　　钱伟长的科学人生：从偏科生到物理专家<br />
<br />
　　钱伟长属于“偏科生”,在数理上一塌糊涂,物理只考了5分,数学、化学共考了20分,英文因没学过是0分。但正是这样一个在文史上极具天赋、数理上极度“瘸腿”的学生,却在一夜之间做出了一个大胆的决定:弃文从理……<br />
<br />
　　钱伟长生平至少2大贡献 参与制定12年科技规划<br />
<br />
