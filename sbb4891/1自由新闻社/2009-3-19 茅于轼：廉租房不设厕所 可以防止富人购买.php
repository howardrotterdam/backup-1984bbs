																											<div id="postmessage_60352" class="t_msgfont"><a href="http://estate.chinanews.com.cn/estate/dcpl/news/2009/03-24/1614412.shtml" target="_blank">http://estate.chinanews.com.cn/e ... 03-24/1614412.shtml</a><br />
<br />
<br />
　　我赞成发展廉租房，但是廉租房应该是低标准的，有私人厨房，但不一定有私人厕所。这引起许多人的质问，为什么廉租房没有厕所。我认为廉租房是有厕所的，而且应该是水洗的，是室内的。但未必是私人的，而是公用的。<br />
<br />
　　这使我联想起经济适用房盖成了180平方米的大公寓。为什么有这种现象？简单说，就是一些有权有势的人侵占中高收入者(他们原本是经济适用房的对象)的利益，以经济适用房为名，图他们的私利。为了防止类似的问题，廉租房应该是低标准的，类似的经济适用房也应该是90平方米以下的。究竟什么样算低标准，大家可以讨论。我建议不设私人厕所只是方案之一。<br />
<br />
　　廉租房是为谁建的？是为在城里生活，收入最低的那部分人，主要是进城打工的农民工。他们现在的住房标准是不得超过15个人一间，这是北京市明文规定的。那儿没有水洗厕所，而且也不是室内厕所。他们能用上水洗的室内厕所就是一个大改进。<br />
<br />
　　低标准的廉租房还有一个优点，就是当他们的收入提高以后，会主动退出廉租房，去寻找条件更好的住房。别的国家实施廉租房的经验之一就是客户条件改善之后不愿意退出廉租房，还想继续享受廉租房的优惠。如果条件差，他们比较容易自动退出，去寻找适合于自己收入水平的住房。<br />
<br />
　　有人说，廉租房没有私人厕所是对穷人的歧视。这话不错。市场经济就是对穷人不利。有钱人什么都能做，没钱什么也做不成。它是认钱不认人的。市场经济能够把饼做大，它的分配原则是谁创造的财富多，谁就得到更多的分配。但是这样的分配原则，不能把饼均匀地分配给每一个人。<br />
<br />
　　廉租房的建筑标准低，既是对穷人的歧视，又是对穷人的帮助和优惠。他们花很少的代价，能够住上比过去好的住房。靠的是全国的纳税人出钱。但是最终走出贫困，还得靠自己努力。把希望寄托在提高廉租房标准上，既不合理，也没有可能。 (茅于轼)</div>

							
							
							
							
