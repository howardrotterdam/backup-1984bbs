------------------------------------<br />
为什么廉租房应该低标准<br />
<br />
天则经济研究所&nbsp;&nbsp;茅于轼<br />
<br />
&nbsp; &nbsp; 我赞成发展廉租房，但是廉租房应该是低标准的，有私人厨房，但不一定有私人厕所。这引起许多人的质问，为什么廉租房没有厕所。我认为廉租房是有厕所的，而且应该是水洗的，是室内的。但未必是私人的，而是公用的。<br />
<br />
&nbsp; &nbsp; 这使我联想起经济适用房盖成了180 平米的大公寓，它不是经济适用房，而是相当阔气的公寓房。为什么有这种现象？简单说，就是一些有权有势的人侵占中高收入者（他们原本是经济适用房的对象）的利益，以经济适用房为名，图他们的私利。为了防止在廉租房中出现类似的问题，廉租房应该是低标准的，类似地，经济适用房也应该是90平米以下的。究竟什么样算低标准，大家可以讨论。我建议不设私人厕所只是方案之一。<br />
<br />
&nbsp; &nbsp; 廉租房是为谁建的？是为在城里生活，收入最低的那部分人，主要是进城打工的农民工。他们现在的住房标准是不得超过15个人一间，这是北京市明文规定的。那儿没有水洗厕所，而且也不是室内厕所。他们能用上水洗的室内厕所就是一个大改进。其实这种情况在改革以前是很普遍的。就在东北，冬天非常冷，上厕所只好去露天挨冻。即使在现在，大部分大学生的宿舍里也没有私人厕所，厕所也是公用的。进城打工的农民工能够住上有私人厨房的廉租房应该感到满意，因为比原来的条件有了很大的改善。<br />
<br />
&nbsp; &nbsp; 低标准的廉租房还有一个优点，就是当他们的收入提高以后，会主动退出廉租房，去寻找条件更好的住房。别的国家实施廉租房的经验之一就是客户条件改善之后不愿意退出廉租房，还想继续享受廉租房的优惠。当局很难请他们走。如果条件差，他们比较容易自动退出，去寻找适合于自己收入水平的住房。<br />
<br />
&nbsp; &nbsp; 改革以前北京（以及许多其他城市）都有所谓的筒子楼。那是一般干部的标准住房。中间一条走道，两边是一间间的宿舍，每间的面积约为15平米。一间房就住一个家庭，有两三口人，做饭就在自己的房门口。他们既没有私人的厨房，也没有私人的厕所。但有公共的室内水洗厕所。我心目中的廉租房就相当于过去的筒子楼。但是厨房还是应该独立隔离，公用厨房是很难的。<br />
<br />
&nbsp; &nbsp; 有人说，廉租房没有私人厕所是对穷人的歧视。这话不错。市场经济就是对穷人不利。有钱人什么都能做，没钱什么也做不成。它是认钱不认人的。能不能改成一视同仁？我们试验过，就是吃大锅饭，结果不但富人完蛋，穷人更惨。倒是改革开放以后，虽然贫富差距扩大，但是穷人的生活有了改善。现在基本上没有人挨饿了。这是中国几千年从未有过的事。市场经济能够把饼做大，它的分配原则是谁创造的财富多，谁就得到更多的分配。但是这样的分配原则，不能把饼均匀地分配给每一个人。<br />
<br />
&nbsp; &nbsp; 廉租房的建筑标准低，既是对穷人的歧视，又是对穷人的帮助和优惠。他们花很少的代价，能够住上比过去好的住房。靠的是全国的纳税人出钱，帮助低收入群众，改善一点生活。但是最终走出贫困，还得靠自己努力。把希望寄托在提高廉租房标准上，既不合理，也没有可能。因为我国还是一个比较穷的国家。将来经济实力进一步提高，可以建高标准的廉租房。（编辑：陆思同）</div>

							
							
							
							
