<br />
　　文章的标题是《开超级跑车的孩子》，讲述了六个二十岁左右的年轻人的故事，他们来自富裕家庭，拥有自己的豪华跑车。 <br />
<br />
　　他们在“超跑俱乐部”聚会交友，加入该俱乐部的“门槛”是拥有最新款的保时捷或者几辆兰博基尼。 <br />
<br />
　　北京光华路上一个报刊亭老板说杂志的七月刊本月第一个星期就到了，但是第二天全部被召回。 <br />
<br />
　　当天卖出了有十来份《GQ》，再版的新杂志里没了这篇文章《开超级跑车的孩子》。 <br />
<br />
　　位于朝阳区华贸中心的《GQ》办公室的五名员工拒绝对此事作出评价，称他们不向媒体透露内部消息。 <br />
<br />
　　最近写了那篇关于“超跑俱乐部”文章的记者说，俱乐部的发起人张宽找了他，并称很可能起诉《GQ》。张宽就是文章中提到的六个年轻人之一。 <br />
<br />
　　据说张宽称《GQ》记者在被采访人毫不知情的情况下录了音，而且公布了不应该录下的内容。 <br />
<br />
　　原文是长达八页的故事专题，开头有两页大图华丽地展示了一个叫“鹏鹏”的年轻人站在一辆白色跑车里。 <br />
<br />
　　文章描述了企业家们的子女的生活，他们追逐模特，在奢侈的夜总会里烂醉如泥。文章使用了实名并且描述细致。 <br />
<br />
　　“他们一边享受着金钱能带来的最好的生活，一边等着继承家族产业。” <br />
<br />
　　文章还讲了一个故事，一个男孩子在聚会时注意到一名漂亮女子，但是当他看到这个女子开着五年前款式但挂着新牌子的保时捷时就决定不理会她了。 <br />
<br />
　　“二手货,” 这个二十六岁的年轻人说。 <br />
<br />
　　“超跑俱乐部”官方网站和博客并没有提及《GQ》那篇文章。一个自称是俱乐部会员，名为“梦牵”的网友在网上发帖指责《GQ》，说文章以清教徒似的高姿态来抨击富二代奢侈风流的生活方式。帖子说，“一个宣传奢侈品的杂志怎么能谴责奢侈的生活方式呢？” <br />
<br />
　　自今年四月份“超跑俱乐部”50个成员把他们的豪车集体停在北京车展会场外开始，这个组织就受到媒体的高度关注。今年六月12辆兰博基尼又在长安街上并驾庆祝一个成员的婚礼，这些照片被发布在网络上第一天就吸引了10万的点击率。 <br />
<br />
　　北京6月30日新闻报道称，“超跑俱乐部”的130个成员拥有总计超过160辆奢侈跑车。</div>

							
							
							
							
