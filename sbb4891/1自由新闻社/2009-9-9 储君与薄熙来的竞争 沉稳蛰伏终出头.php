													<h2>薄熙来攀比Xi Jinping</h2>
						
						
																											<div id="postmessage_232830" class="t_msgfont">前哨杂志<br />
姜维平<br />
<br />
今年7月30日，薄熙来在重庆做了一件惊人动地的大事：为纪念中国人民解放军建军82周年，举办了“红色经典歌曲演唱会”，此举非同小可，因为他在会上不仅邀请了贺龙的女儿贺黎明与母亲薛明，给足了她们面子，而且还就邓小平太太卓琳去世一事，发表了一篇异乎寻常的即席讲话，第一次强调“团结在以Hu Jintao为首的党中央周围”，由此透露出中共高层在新疆事件之后产生的分裂与内斗，以及两派均向军队争宠的信息。所以这次演唱会如同此前薄熙来进京主办会议，高调纪念“中共中央南方局”一样，既是向中央高层示威，又是在与“储君”Xi Jinping攀比，充分暴露了他争当中共一把手的政治野心。<br />
&nbsp; &nbsp; 嫉恨Xi Jinping由来已久薄熙来是恢复高考后，通过全国77级第一批通考进入北大校园的，虽然他分数差12分不够录取，但薄一波求情于邓小平，邓秘书一句话，走后门进了校门，尽管如此，他也瞧不起Xi Jinping。因为习是“工农兵学员”。早在1984年底，薄刚从北京空降到大连金县{后改金州区}任副书记后不久，在金州宾馆举办的一次“小哥们沙龙”聚会上，他的一个哥们问他，听说Xi Jinping也想学习李向南{电视剧《新星》中的县委书记}，他到了河北省正定县当干部？薄熙来那时正被大连市副市长唐某舜排斥，小哥们沙龙才6个人，所以他不敢得罪这批所谓“患难之交”，性情还算温和，他显出不屑一顾的神情说，他算个什么？工农兵学员！。。。。。。此时，与薄同时曾于1982年在国务院办公厅任职的Xi Jinping，下派到河北省正定县当副书记，与薄开始了争当“革命红色接班人”的马拉松式长跑，他们同时起步，同一个目标，.但命中注定不能同一个终点。从1984年到2000年底，我可以证明，虽然邓小平之子，王震之子，陈云之孙，李铁映之子，叶挺之子等，许多太子党都争先恐后去大连掏金，薄用廉价土地等经济利益抵消或稀释了他们父辈之间的恩怨，与其结成了既得利益集团，但Xi Jinping与他绝无任何往来，我印象中薄从未在大连接待过Xi Jinping，也未曾有过此类耳语。<br />
&nbsp; &nbsp;&nbsp;&nbsp;俗话讲：同行是怨家。别看习薄都是太子党，但他们之间早已存在著很深的芥蒂。这一点从薄在1988年后任中共大连市委宣传部长期间的言谈举止中可以断定。当时市委书记毕某桢等人看不上他，逼得他靠近主管宣传的副书记于学祥，而且更把几个处级干部当成知已。据接近薄的两个处长王某强与张某某披露，他们说他最恨江青，毛泽东，因为他们家的人都受到这两个人的残酷迫害，不仅丢了官职，而且还坐了大牢，但邓小平不敢全盘否认毛，就把建国后的坏事全推到江青等“四人帮”身上，这样才赢得了人心。并说，我们都是这么明智。在谈到坐牢时，薄说他吃了很多苦，不仅吃不饱，还被狱老大打骂，有一次把他膝盖骨打坏了。。。。。。在谈到张锦华{与其同时到大连金州任职，当时传说是张廷发的儿子}Xi Jinping,王军，王兵{王震之子}时，薄熙来说，他们不算什么，他们哪个坐过牢，经受过象我这样的锻炼？。。。。。。其蔑视与自负的口气，一如当年。 (博讯 boxun.com)<br />
<br />
当1985年，Xi Jinping还徘徊在河北正定县委书记的位置上，不知前程如何时，薄熙来已跃升为中国14个沿海对外开放城市之冠的大连市的市长，他用瞧不起人的目光，环视中国政坛，已长长地舒了一口气。他对金州的哥们说，与其同时起步的Xi Jinping，已不是他的竟争对手了。当2000年至2002年Xi Jinping任福建省委副书记，代省长时，薄熙来早已把辽宁省长前面的“代”字扔掉了，他对跟随他的“金州帮”小哥们说，中国下一步就是我们的了，你们都要胸有大志！国务院各个部都等你们呢。显然嫉恨Xi Jinping的薄省长，已把习之类的太子党排斥在中国的“我们”之外。<br />
&nbsp; &nbsp; 闻世震成了薄熙来的克星2004年9月4曰出版的《时代人物周刊》，以令人肉麻的语言，大肆捧杀了薄熙来，题目竟是“薄熙来的5种面孔”，试想，一个人有一个脸足够了，叫“言行如一”，两个脸叫“两面派”，5个脸呢，叫“八面伶珑”。这还了得！文章称薄善于团结人，这显然不对。从1988年当上大连市委常委后，他与班子里所有的常委都合不来，与宫明程，唐某舜，魏富海，高姿，傅万忠等人相处，无一例外。然而，有他父亲在北京坐阵，每次内斗他大都是胜者，不过，2004年初，在与辽宁省委书记闻世震的权斗中，薄却最后败在了他的手下。这一年是Xi Jinping在淅江的仕途转运走红的一年，但对薄熙来则是遭遭了命运的“滑铁卢之役”。因此我认为他并没有做到“八面玲珑”，却正好相反。<br />
&nbsp; &nbsp; 早在中共15大之前，薄家就上演了一场笑人的闹剧。薄令著名女作家陈某芬撰写了题为《世界上什么事最开心》一书，为自巳大肆吹捧造舆论，其父在山西则向希望小学捐款30万元，以此收买人心，于是讧泽民破例给了他一个15大列席代表的名额，并规定他有中央委员或候补委员的被选举权，但闻世震为首的辽宁代表团集体抵制他，会议期间谁也不理他，选举时他一票难求，悻悻然返回大连大病了一场，连谷开来都不到他住院的大连友谊医院去看他，他成了世界上“最不开心的人”。也就是说，当他面向辽宁省委的高位展望时，与Xi Jinping正好相反，Xi Jinping以为人憨厚谦和，低调理性而争取了福建与浙江的民心，越走越顺，他则以奸诈阴险，华而不实而与同僚争斗不息，恂私枉法，渐趋孤立。到了上个世纪末，虽然原中共辽宁省委书记顾金池，迫于京城权贵的压力，在众多地方官员阻拦薄上升的臂膀中，为其挣开一条缝子，但闻世震及其追随者，仍然以国企改革较为成功的实绩与“慕马大案”查处后的自身清廉而把他挡在封疆大吏之外。这使薄成了上调商务部而未能成为地方一把手的又一个官员，此前官场有议论说，凡是在省一级当不上书记进京当部长或下派再升的人，无一最终的命运不是走麦城，比如田凤山，张国光等人均是也。<br />
&nbsp; &nbsp; 毫无疑问，1999年对薄与习都是关健的一年，习是福州市委书记，薄是大连市委书记，他们又站到一个平台上，驻足远望。Jiang Zemin已无力改变邓小平隔代搞定的接班人Hu Jintao，但大连之行，却因为薄的忠心而使昏庸的Jiang Zemin如梦初醒{薄在大连中山路高挂江的巨幅画像}，故他己决定贬胡举薄，奋力为薄熙来护航，而Xi Jinping更是被其暂放一边。于是，国安部与中纪委抓住了沈阳市副市长马向东的赌博案，大做文章。实际上他的丑闻与其它人的黑材料一样，早就进入了特务们的视线，Jiang Zemin，尉健行，李铁映的用意，是用反腐这把利剑，为薄上升杀出一条血路，原籍沈阳的中纪委副书记刘丽英派上大用场，按照薄的想像，马向东贪，慕馁新贪，张国光贪，闻世震还能不贪？一路下来，在辽宁省第9次党代会上夺权，已稳操胜券。所以，Jiang Zemin亲自提议薄当上了辽宁省长，并在16大上又当上了中央委员。这时，薄回头一望，与Xi Jinping又并驾齐驱了，不由得惊出了一身冷汗。<br />
&nbsp; &nbsp; 然而峰回路转，阻力并未排除。一方面专案组抓了省长张国光，却并未查出闻世震的经济问题，这个从大连国企基层一步一个台阶，爬上来的省委书记，不仅懂得管理国企云集的一个东北大省，而且知道如何抓住薄与黑社会与大款勾结的软肋，与其巧妙周旋。他利用大连邹显为{虎豹}黑社会团伙案，沈阳杨斌案，仰融案等，把薄熙来斗得丢盔御甲，声名狼藉，只能“三十六计走为上”了。<br />
&nbsp; &nbsp; 2004年2月16日，正当薄熙来大喊“振兴东北经济”之时，中共中央急令其赴京待命，他想到了后来居上的Xi Jinping，这时他巳是浙江省委书记兼人大主任，而自已则腹背受敌，前程未卜。他忐忑不安地等了13天，有时竞担心会因贪腐与枉法而被双规，吓得彻夜不眠，他在大连的一个金州时的死党说，胡斗败了江，薄必死无疑。然而事有转机，2月29曰他被任命为商务部长。次日，他就兴奋地上班了。<br />
&nbsp; &nbsp; 不料后院被Li Keqiang逛个遍胡温把薄熙来调出辽宁时，我还在狱中，当然我不可能了解幕后的隐情，但2006年初我获释后，北京新闻界一个与我有多年交情的资深记者对我讲，早在17大之前，有关胡将重用Li Keqiang的传闻己甚嚣尘上，而Xi Jinping则无人论及，新任商务部长的薄熙来正跃跃欲试，但胡不动声色地让65岁到点的闻世震退二线，改任全国人大财经委副主任，让河南省委书记Li Keqiang到辽宁走马上任，其目地是要他调查薄的历史问题，把他的尾巴抓在手里，伺机行事。<br />
&nbsp; &nbsp; 上任第二天，Li Keqiang推开秘书与司机，自已一个人亲自驾车到新华社辽宁分社，走遍了每个办公室，看望了每一个记者，并和从福建分社调来的新任社长费某开怀畅谈。{此人在福州与Xi Jinping关系密切}辽宁分社一个记者对我说，许多人此后与李经常约谈，其内容自然与薄有关。薄欺上瞒下，弄虚做假已使记者们厌恶。特别是大连信访局外设有专门抓捕访民的“车体办公亭”，多年以来流动作业，群众愤恨，曾被分社驻连记者李某林的报道批评过，薄熙来孙春兰等历任地方官员均怨恨记者，不处分警察，却逼大连支社领导换人，这惹得记者愤愤不平。有记者说，抓了姜维平，还想抓我们？姜把文章发在香港，坐了冤狱，我们发在《国内动态清样》上，也犯法吗？。。。。。。这些反映都使Li Keqiang震惊。他更感肩上担负的重任很是艰巨。<br />
&nbsp; &nbsp; 果然，Li Keqiang在辽宁，此后不久，不仅一举摧毁了薄熙来伞荫下的大连黑社会老大冬海波，牵址出一大批大连以至北京的贪官，而且把薄离开大连前安插在市一级领导岗位的死党，一个个都调任它职，如薄在大连大搞冤假错案，做为戴笠般打手的原大连政法委书记成城，则被其仿照当年薄整政敌的惯用办法，贬任市政府副秘书长，副市长董文杰，宋增彬等人，均被免去了职务。尤其是他还掌握了王奉友创办的蚁力神公司非法集资的情况，对薄与王奉友等众多大款勾结的私下交易也大都记录在案，留待曰后适当时机再清算。正如大连日报一位记者说的那样，胡把薄明升暗降，再把李派下去“掏地沟”，在辽宁这个薄熙来苦心经营了20年的“私家小院”逛了个遍，尔后才把Li Keqiang调到国务院任要职，不能不说是深思熟虑。<br />
&nbsp; &nbsp; 我认为这个老记的话真是一针见血。因为中共拒绝政治体制政革，党内的权斗又不能公开化与合法化，所以便只能以反腐倡廉为借口与筹码，进行内部权力交替，这样一来，抓在Li Keqiang手中的有关薄的罪证，就成了逼其倒向胡的把柄。这就是为什么薄在当了商务部长之后，亦步亦趋地跟随胡出访，一反常态，颇似忠诚的主要原因。<br />
&nbsp; &nbsp; 据北京新闻界消息人士称，2003年底与2004年初，薄熙来与闻世震的矛盾巳表面化与公开化，闻世震指责他弄虚作假，大搞政绩工程，把城市建的象欧洲，农村象非洲，薄反驳他纵容包庇慕，马，张等贪官，造成社会两极分化，干群关系紧张，两人在省常委会上争吵不休，有时还摔文件，拍桌子，踢凳子以至对骂，尔后双双去京告状，为此中央十分为难，最后不得不下决心做人事调整，薄提出要当外交部长，被胡温以其与FLG结怨出访不便为由拒绝，后改任商务部长。<br />
&nbsp; &nbsp; 实际上，薄心里虽然恐慌，但心中也有底：只要老爹不闭眼，Jiang Zemin也好，Hu Jintao也罢，都要给他面子。不过Li Keqiang把大量有关薄的举报信，放在抽屉里，偶尔也会有人透露一点给薄，让他知道该怎么做，这叫“敲山震虎”。所以，薄不止一次地对原金卅时的秘书，大连国安局书记车克民说，谁跟我上北京，你都不能动，因为Li Keqiang来者不善，善者不来，你要紧紧盯住他，离开大连国安，我们就成了聋子了！死都不知怎么死的。<br />
&nbsp; &nbsp; 而此时，Xi Jinping正在浙江省委书记的位置上，低调行事，静观其变。2007年初Xi Jinping的密友，新华社辽宁分社新任社长费某说，他原在福建分社任职，很了解他。接近费某的我的一位朋友转述说，Xi Jinping从未想过要当中共中央下一代领导核心人物，他不象薄那样野心勃勃，当然与同事也少有矛盾。所以上升的路走得很顺。正因为如此，薄熙来产生了危机感与紧迫感，决定破釜沉舟，大干一场。<br />
&nbsp; &nbsp; 迫不急待地要当国务院副总理2004年2月当了商务部长之后，薄熙来鼓足了劲，要胜过Xi Jinping等人，干出一番事业来，爬上副总理的高位。薄在大连的死党预测，17大他一定能进政治局，并任总理或副总理。一方面薄在读新闻研究生时的同学朋友，替他在人民网等许多媒体大造声势，上述《时代人物周刊》即是突出代表，其文赞他“俊朗，优雅，开放”，是“上升势头强劲的明星”。德国的《世界报》吹捧他是“稳定执政者领导地位的推动者”。另一方面，他奔波在全世界的政治舞台上，不论是陪胡出访，还是伴温巡行，都声肆力竭地充分表现自已，给外人一种喧宾夺主呼之欲出的感觉。这些行动都透露出其内心的焦虑，说明他抢班夺权已急不可耐。<br />
&nbsp; &nbsp; 以2004年初的美国之行为例，他与国务院副总理吴仪同行，明显是个配角，而且不论吴还是他，在3天的友好协商中，与美方签署的8个协议，原则上想必是胡温的主见，但后来由薄传达给媒体的消息却说，是他的“务实，幽默，机敏“，“促成了谈判的顺利进行”，这不仅歪曲了事实，而且夸张了个人的作用，使“铁娘子”吴仪很是生气。北京一位原籍大连的高官对我说，他把吴仪当成了孙世菊{原金州副县长，后大连市政府秘书长}，以部长的身份指挥人家副总理，贪天之功归已有，薄熙来简直疯了。其实，中美贸易逆差1246亿元，这个问题肯定要解决，谁当时去谈都要面对双赢的现实，薄急于与Xi Jinping等人争夺接班人，已利令智昏，造成与吴仪结怨，又多了一个政敌。<br />
&nbsp; &nbsp; 此后不久，他又陪Wen Jiabao到欧盟5国巡访，.他又抢风头，乱讲话，竟在一次外事场合，呼吁欧盟给予中国市场经济地位，这原本并没有错，但问题是这个建议应当由总理Wen Jiabao讲，因为只有他才能代表中国。薄这种错位的表现，有失自已的身份，不仅给国际社会造成误解，而且使温及代表团其它人十分尴尬。还有一次，他做了一次有辱国格的表演，在另一个外事场合，他为了显示自已懂点英文，竟当场直接与外方对话，还戏弄译员，不以为耻反以为荣，这些举动都令人匪夷所思。我还在2006年初读到了一则旧闻，他在波兰访问时，有个记者问及我的案件，他竟以造谣诽谤的方式污辱我的人格，忘记了出访5国的商务重任，充分显示了他小肚鸡肠借公泄愤的恶劣品质。然而，被其表面上夸夸其谈蒙住了眼睛的一家媒体，却赞扬他将是“第一届中国民主改革成功后的首任总统”。连国内上述杂志也预测，“再过3年，不知道还会带来什么惊喜”。真是可笑之极。<br />
&nbsp; &nbsp; 由于薄熙来在党内外，毫不掩饰地吹嘘包装自我，要挤进中央权力核心层，使胡温十分警惕，所以2005年10月8日至11日，在举行中共中央16届5中全会时，以祝贺朝鲜劳动党60周年庆典为名，把他与吴仪派出国外访问。有趣的是，在与金正曰的会谈中，他恰恰坐在金的左边。这是一个巧合，但正好也可以通过电视告诉全世界的人，薄熙来假如有一天独掌了中国大权，就会比金正曰搞的那一套还左。<br />
&nbsp; &nbsp; 辽宁“蚁民”咬伤了薄熙来在多年的官场上勾心斗角拼搏撕杀，薄熙来深知金钱的魔力，他不仅指使太太创办律师所捞钱备用，还通过大连廉价批地的方式送人情，结交了政界商界一大批权贵。但他要想继续上升，这些权贵们贪婪的胃口还需继续填饱。所以，他到北京商务部任职后，继续与旧日结交的大连沈阳两地老板保持密切往来，如大连大商集团的董事局主席牛某，大连新型集团老板孙某科，大连珍奥生物工程有限公司老板陈某松，大连大杨集团董事长李某莲，大连金广建设集团老板范某臣，大连万达集团老板王某林，大连实德集团老板徐某，还有沈阳蚁力神老板王奉友等，这些人在中共17大之前，都不仅共同编织着薄熙来当上了国务院副总理的美梦，而且在大连与北京之间频繁活动，为其忙的不可开交。大连坊间流传说，薄的死党们扬言，要凑合10个亿，搞定所有的中共17大代表，非把薄熙来选进中央政治局不可。但此议论一时不便证实。<br />
&nbsp; &nbsp; 与此同时，在浙江新闻界工作的一位老记说，当地对Xi Jinping的议论一如往昔，没听谁说过他会在中共中央成为下一代领导核心人物。这一点与大连民情截然不同。<br />
&nbsp; &nbsp; 据大连新闻界消息人士透露，薄的死党宋某某，被辙了副市长职务之后，索性辞去了公职，带领10几个薄的“金州帮”小哥们，带着巨款与银联卡前往北京，成立了专为薄竟选的不挂牌的办公室，四处活动，请客送礼，上窜下跳，大拉选票，为薄熙来尽效犬马之力。大连薄的一个哥们对我说，我们已经把不少17大代表买通了。<br />
&nbsp; &nbsp; 此前，薄熙来利用商务部长的便利条件，不仅在2005年5月21日，亲自到大连大商集团视察打气，而且还给了大连大商集团许多优惠条件，使其董事局主席牛某通过企业兼并，收购扩张，资本运营，合资合作等，把东北三省的所有大商场零售业几乎全部收入囊中，在11个省50多个城市，建了150个店铺，2008年营业额多达625亿元，成为全国最大零售业集团。牛某说，90年代初就是薄市长，用股份制救了大商，现在把我们变成了全国零售业老大。我们感谢他啊！{他是如何感谢的，读者可参阅《前哨》8期}同时，薄又破例给了大连珍奥生物工程有限公司一项零售直销权，使他当年亲自为其做过宣传的这家民营企业大发横财，成为世界级核酸产业基地。此外还有90年代初他在大连，以土地优惠政策扶植起来的万某集团，更是以雄厚的经济实力，进军国际商界，成为拥有商业地产，文化产业，高级酒店，连锁百货为4大支柱的大型企业集团，现已资产多达400亿元。这些人及企业都变成了他的社会经济基础。<br />
&nbsp; &nbsp; 果然，在中共17大上，薄熙来挤进了中央政治局，坐上最末一把交椅，但被阻挡在政治局常委的门外，特别是名声不大的Xi Jinping，后来居上，成为新杀出的一匹政坛的黑马，即“储君”，这令薄熙来倍觉凄凉与失望，他一再对大连的哥们感叹，老爷子走得早了点，假如能活过17大党代会，胡温哪个敢不给中共元老面子？他越想越咽不下这口气，又象15大过后的心情一样，很是郁闷。<br />
&nbsp; &nbsp; 但这一回他没有病倒，并非变得坚强了，而是党代会过后，马上便是全国人大选举，薄对云集北京给他打气的大连哥们说，不要灰心，要花更多的钱，打通更多的关系，盯住每一个代表，一个个搞定，一定要把副总理的位子拿到手！立即，这些捧臭脚的人，日夜不停地继续奔波，自以为胸有成竹。然而不料在这个关健的节骨眼上，竟在辽宁惊爆了数万人围困省政府大楼的“蚁力神事件”，而这个骗子公司正是当年薄熙来在沈阳精心扶植起来的，被抓捕的董事长王奉友则是他的铁哥们，显然这个群体性事件偏在此时钻了出来，耐人寻味。薄熙来真是不走运，以往累积成山的他所干的坏事，仿佛在一个十字路口等着他，他要前行，却逼他拐弯，从此他走了下坡路。过去他理也不理的“蚁民”们给了他一个拐点。果然，在全国人代会上，他不但没当上副总理，连个国务委员都没靠上边，他气急败坏了地回家躺了整整4天，病得昏天黑地。他对登门安慰自已的大连铁哥们说，我主要是栽在Li Keqiang与张文岳这两个王八蛋手中，他们把蚁力神的事查清了之后，一直抓在手里，专等关健时刻把蚁民们放出来咬我，这下完了，Hu Jintao在背后操纵，Xi Jinping看我的笑话啊！他的众多从大连赶来的支持者还给他打气，对他说，走到这一步，不进则退，退则无路，当年你在大连整进监狱的团派干部刘晓滨，恢复了副局级干部，到旅游局当了副局长，高姿也解除了“双规”，正在上告，天天渔港坐牢的4兄弟全放了，也在申诉，这些恨我们的人都不会放过我们呀！。。。。。。于是他们又紧急磋商了下一步自保的方案。<br />
&nbsp; &nbsp; 转变策略，刮起重庆红色风暴新的中央领导集体做出诀定，要搞西部大开发，并以薄具有开放意识与开发经验为由，把汪洋升为广东省委书记，却把他下派到重庆当一把手，虽然胡温对他表面上是委以重任，但实际上是担心他与Xi Jinping攀比，在北京的中南海里闹地震，而重庆处于内地，便于防止他搞政变与权斗，也便于派特务监控他，这一点他心中有数，但又无计可施。最初，他拒绝离京，提出要回到辽宁当书记，但一方面辽宁的官员与人民对其痛恨不已，坚决反对他，如硬强派，必惹众怒，另一方面挂个政治局委员的名头，不去直辖市任职，也说不过去，薄熙来又不敢顶撞胡温，他深知他主政辽宁20年间，上告他的黑材料足够装满10个麻袋，光王奉友蚁力神一案，真的摆到桌面上，就够判他10年有期徒刑，而Li Keqiang，Hu Jintao早把这一切放在抽屉里。。。。。。于是，当Hu Jintao2007年9月，把以前在沈阳军区工作时与薄熙来熟悉的驻渝军区司令员王某民调离之后，薄就不得不去重庆上班了。<br />
