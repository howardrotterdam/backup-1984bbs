													<h2>储君与薄熙来的竞争 沉稳蛰伏终出头</h2>
						
						
																											<div id="postmessage_232829" class="t_msgfont">来源：联合早报 2009年9月09日<br />
<br />
Xi Jinping和薄熙来两人的父亲都当过中国副总理。年幼时，他们被称为“高干子弟”；如今身居高位，被称为“太子党”。Xi Jinping1953年6月出生，现为中共中央政治局常委、国家副主席；薄熙来1949年7月出生，现为中共中央政治局委员、重庆市委书记。Xi Jinping有望在 2012年中共十八大后执掌中国，而年长四岁的薄熙来在2012年后能否更上一层楼，至今还是未知数。 <br />
<br />
Xi Jinping尚在中央蛰伏，一如既往的四平八稳，始终没有什么显山露水的表现；薄熙来则在地方大展拳脚，干得风生水起，近期更是以重庆“打黑”吸引了全国人的眼球。网上不少网民“跪求”，希望薄熙来去他们那里打黑，或者出任下一届总理。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; “太子党”中最引人注目的这两人，反差可谓大矣：一个藏拙，一个显能，似乎在暗自进行一种较量。尽管在中央政治局常委的未来布局中，被聚焦最多的竞争对手是Xi Jinping和Li Keqiang，而非Xi Jinping与薄熙来，但“红色第二代”中的一博，并非没有看点。至今的格局显示，虽然Xi Jinping与薄熙来同为“红二代”，但Xi Jinping这个 “老实人”比薄熙来这个“能人”，显然更让退下来的老人们放心。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 中国的未来，是更需要“老实人”还是更需要“能人”，这是值得寻味的。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 家教、性格、经历各不同<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 在过去二十多年的仕途中，Xi Jinping总是跑在前头，薄熙来跟随其后不懈紧追。2001年眼见刚刚追上，不料想Xi Jinping在2007年被高人猛然推进中南海，成为中共“储君”。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 薄熙来的“中南海梦”屡试不爽。据称，2002年和2007年中共十六大和十七大期间，他曾有两次机会冲击国务院副总理，但无奈反对者众，不得入中南海之门。特别是2007年十七大前，身为商务部长的他，与前副总理姚依林的女婿、时任北京市长王歧山竞争即将退休的吴仪副总理之后任，结果败给王歧山。在此，暂且不表薄熙来与王岐山之输赢，倒想探讨一下造成Xi Jinping与薄熙来巨差的原因。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; Xi Jinping是家中长子，父亲习仲勋要求他从小就要朴素勤俭，过百姓生活，甚至在他上学后仍要求他穿姐姐剩下的鞋，把花布鞋染黑了再穿。据笔者一位与Xi Jinping同过窗的朋友透露，习从小就是一付少年老成的样子，很实在、听话，极会克制自己，即使别人踢他两脚，他也不会还脚。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 薄熙来是家中次子，父亲薄一波对他的成长比较放任，薄家不像习家有那么多约束。文革期间，他家的工作人员以大字报揭发薄家孩子生活优越，少年时就拥有手表、自行车、收音机，对保姆和工作人员没有“阶级感情”。薄熙来的发小（儿时玩伴）透露，他从小就非常机灵，爱耍小聪明，在学校也从来不肯吃亏。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 习家与薄家虽然同是红色背景，但通过上述简短描述已可看出，家风与教育方式截然不同。另外，薄熙来一表人才，聪明的头脑加英俊的外貌，令他更加自信。Xi Jinping小眼睛大鼻子，其貌不扬，也使他或多或少缺乏薄熙来那种气宇轩昂的自信。<br />
&nbsp; &nbsp;&nbsp;&nbsp;更大的不同是：Xi Jinping9岁时，习仲勋因一本名为《刘志丹》的小说丢了副总理，被撤销党内外一切职务，先后被关押16年。习家的生活从阳光进入黑暗，小小的Xi Jinping已开始痛感政治的危险与世态的炎凉，他以后超常的谨慎与忍耐恐怕与此有关。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 薄熙来直到“文革”爆发、其父被卷入“61人叛徒集团案”遭受迫害前，一直生活在无忧无虑的幸福中。当他因“黑帮子女”落难时，已是十七八岁，精神上的痛苦自然比Xi Jinping强烈，后来他还被关了起来，被强迫劳动。不过，更令薄熙来痛苦的是母亲胡明在文革中自杀身亡。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 文革中，为响应毛主席“广阔天地，大有作为”的号召，同时避开北京的政治风暴，刚刚16岁的Xi Jinping去父亲的故乡陕西省农村插队，1969年到1975年的6年时间里，在延川县文安驿公社梁家河大队从知青做上支部书记，后来还被推举上了清华大学，这里有他的苦干，也有他父亲的福荫（他父亲虽然被打倒，但在农民心里仍是大人物）。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 薄熙来没有下乡插队的经历，但4年的所谓“学习班”劳教，劳动强度恐怕也不逊于艰苦的陕北。不过他1972年就进了北京五金机修厂当工人，这几年比Xi Jinping要轻松自在。所以他是在读研究生期间入党，比Xi Jinping晚了6年多。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 一个高调张扬，一个低调沉稳<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 红色后代从政者中，上世纪八十年代最出名的当属时任河南省副省长刘源（刘少奇之子）和北京市副市长陈昊苏（陈毅之子）。后来两人受低选票影响，九十年代初不得不转换阵地转入低调，自此政坛中的“太子党”被罩着阴影。那时，习、薄两人虽然位级较低，但压力相当。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; “性格决定命运”。这在Xi Jinping与薄熙来身上，呈现出泾渭分明的走向。当“太子党”再次活跃时，习、薄成了最被看好的两位。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; Xi Jinping1993年当福建省委常委和福州市委书记时低调默干，绝不当出头鸟，除了福州百姓和圈内人外，没什么人知道他。薄熙来则不然，1993年当上大连市长即高调明干，那一年笔者就见识了他率团来北京宣传要在大连建“北方香港”的大气魄，其后大连果然干得风风火火，他的成功在全国远播。著名报告文学作家陈祖芬以长篇幅写他，凤凰卫视采访他，国内记者热棒他，香港等境外媒体也热追他。他成了全国最璀灿的政治明星。当时，Xi Jinping的名气和政绩远不能与薄熙来相比。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 薄熙来超常的宏大气魄，让他与过分张扬、自以为是相伴，引起政治生态中的同类排斥：1997年中共十五界中央委员选举，薄熙来遭遇滑铁卢，“太子党”和“太高调”成了他之觞。与此同时，低调与圆厚的Xi Jinping当选中央候补委员。这似乎应验了命运由性格决定之规律。<br />
&nbsp; &nbsp;&nbsp;&nbsp;曾庆红举荐　Xi Jinping青云直上<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; Xi Jinping为什么能够一飞冲天？是谁把他发射上去的？<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 通过种种现象和线索分析，曾庆红起了重要作用。八十年代初，Xi Jinping在中南海为耿彪当秘书时，曾庆红为副总理兼国家计委主任余秋里当秘书。无疑，Xi Jinping的本分、实在，给曾庆红留下良好印象。另外，Xi Jinping之父习仲勋担任副总理时，曾庆红之父曾山担任内务部长，两家本来就认识。曾庆红比Xi Jinping大14岁，对这位小老弟，算是欣赏有加。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 前上海市委书记陈良宇倒台后，谁来接任是个伤脑筋的难题。Hu Jintao顾虑人言，无意安排团派人选；Jiang Zemin虽仍具影响力，但陈良宇丢其颜面，令他无力再荐“海派”，然心又不甘。于是，曾庆红乘机点将Xi Jinping。习虽为太子党人，但门风朴素，为人谦逊，扎实肯干，有多年地方管理经验，最终成为双方都能接受的人物。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 十七大前的人事布局中，曾庆红处于两难境地：江希望他下，因他后来疏江亲胡；胡希望他留，因他协调能力超凡，他的年龄处于可下可留之间。曾庆红不愧为“智多星”，在权衡之后主动请辞，不顾胡温挽留，理由是为未来交接班考虑，让位于年轻人。他声言效忠党务，要以“伯乐”之姿挖掘人才，最后挖到了Xi Jinping。</div>

							
							
							
							
