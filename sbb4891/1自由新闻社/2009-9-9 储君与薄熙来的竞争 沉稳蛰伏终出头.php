&nbsp; &nbsp; 不过这件事又弄巧成拙了。北京一位老记说，人家Xi Jinping送女儿去地震灾区献爱心，毛泽东送儿子毛岸英上朝鲜战场，薄熙来送两个儿子到国外读书，这不能同日而语！<br />
&nbsp; &nbsp; 为了体现君权神授，给下一步自已的上升增点神秘色彩，薄又操控重庆新闻媒体，搞出了“万州惊现人头石”的新闻，说是有一个高达68米的石身人面像，横空出世，是“擎天柱之头”，生怕老百姓读不懂，又说“眉头紧锁，眼窝深陷，鼻沟高挺，嘴唇突出，酷似一年轻帅哥”，真是再加上姓薄，便封建帝王呼之欲出了。大连的一个记者表示，过去在大连金石滩，薄属牛，建了青铜的开荒牛雕像，在劳动公园建了“百年世纪仓”，把自己的手书信件与Jiang Zemin给大连百年建市的题辞一并封存，埋入地下，以传千古。他还在大连星诲湾百年城雕里，留下了自已的铜铸脚印。。。。。。这次在重庆他不敢明目张胆这样做，只好借“帅哥石”自慰，真是可笑荒唐之极。<br />
&nbsp; &nbsp; 天怒人怨，政变的可能性有多大今年9月，中共17届4中全会将拉帷幕，外界普遍预计，Xi Jinping可能任军委副主席，那么与其攀比的薄熙来呢，近期他的噪动不安，则增加了中共高层政局的不稳定性。新疆事件之后，Hu Jintao中断出访回国，说明了国内形势已是危机四伏。7月30日薄在重庆搞的“红色经典歌曲演唱会”，调派了驻渝官兵4000人参加，市委市政府成都军区重庆警备区的主要领导都应邀出席，这哪是文艺活动？分明是一次军事政变的彩排。紧接着，薄策划的所谓“将军后代合唱团”又出了笼，其中有150个将军之后联手登台，有陈毅之子陈昊苏，聂荣臻之子聂力，徐海东之女徐文惠，罗荣桓之子罗东进，罗瑞聊之子罗箭，李先念之女李紫阳等，这些人名义上是重复50年前“开国元勋将军合唱团”的旧事，实际上是在向中共中央显示薄熙来的凝聚力，亦是向他们施加压力：如果不在18大上让权，别说部队跟薄走！<br />
&nbsp; &nbsp; 然而，尽管薄熙来煞费苦心，但老天似乎不帮忙。自他入主重庆以来，天灾人祸不断，一会儿出租汽车罢工，一会儿三峡移民闹事，这边上访人员喊冤自焚，那边刁民跑到部队抢枪，又是矿难死人，又是洪水泛滥，又是律师被打，又是驴友坠崖，一方面高考民族身份造假，新华社穷追猛打，一方面路桥费跃居全国第一，市民网上不依不挠。。。。。。单是去年重庆就发生了安全事故2万起，直接经损失12个亿，相当于重庆10万个民工外出打工21年的收入，这是多么触目惊心！与Xi Jinping同时起步比拼的薄熙来，还不得不把这个数据上报他那里去批示，可想而知心情如何？<br />
&nbsp; &nbsp; 不过薄熙来可不是吃素的！他早在90年代初当市长时就讲过，每到本命年，他就要与人顶牛，就要刮起一场台风，而今年华甲牛年，他又要刮什么风呢？上述“将军合唱团”也好，“红色经典演唱会”也罢，没有一个不与部队有关。这说明薄熙来有可能抓住军队，笼络军心，搞“陈桥兵变”。<br />
&nbsp; &nbsp; 近期香港出版的《镜报》月刊披露说，49年建国以来首次军队体制改革即将开始，即撒消以前的7大军区，改建4个战略区，每区将领由军头与各省大员组成小军委，而小军委的一把手由中央任命，显然这个变化有利于薄熙来，南部战略区如果到了他的手中，不仅重庆等地在其领导之下，而且广州军区，成都军区，云贵两省以及所辖的南海舰队，空军，二炮，武警等，均在他的掌控之中。那么，部队合唱团可能就要变调了，胡温习李，都将危在旦夕，中国将进入新的军阀混战的时代，陷入内战，百姓遭殃，绝非危言耸听！<br />
&nbsp; &nbsp; 好在，据笔者观察，在7月30日的会上，新被Hu Jintao授衔的上将张海阳不曾出席，在北京举办的纪念中共中央南方局的会上，虽有宋平参加助威，但在京的中央领导干部，无人捧场，只有薄自已唱独角戏，这说明Hu JintaoXi Jinping都对其心存戒备。<br />
&nbsp; &nbsp; 我认为，17届4中全会召开在即，18大又将临近，薄熙来抓住最后一根稻草的最后的疯狂，总会有一个结果，我相信这个在辽宁横行霸道罪恶累累的人，必将与其前岳父李雪峰一样，倒在政治局委员的位子上，接受人民的审判，这是他的宿命，也是历史的必然。<br />
