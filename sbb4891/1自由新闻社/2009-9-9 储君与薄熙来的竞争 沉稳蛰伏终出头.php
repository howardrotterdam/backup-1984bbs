																											<div id="postmessage_239115" class="t_msgfont">2009年9月11日 星期五 <br />
&nbsp;&nbsp;<br />
物极必反 薄熙来近日发狂妒忌汪洋 <br />
&nbsp;&nbsp;<br />
门礼瞰 <br />
&nbsp;&nbsp;<br />
【人民报消息】还有三天就是四中全会，Jiang Zemin和曾庆红正在帮薄熙来，这俩的主攻对像是Xi Jinping，而薄熙来仇恨前重庆市委书记、现广东省委书记汪洋，完全是出于妒忌。<br />
● 薄熙来妒忌汪洋没道理 <br />
<br />
按理来说，下届政治局常委的空位很多，不像曾庆红和罗干当年为进十六届政治局常委会，争吵到一个进了医院，另一个威胁要撂挑子。但薄熙来有个与曾庆红在十七大同病相怜的问题，就是年龄。其它条件不讲，单单这一个问题就要了薄熙来的命，就能把他卡在被考虑人选之外。1955年3月出生的汪洋比1949年7月出生的薄熙来小整整5年零8个月，怎么掰手指头算，汪洋也不存在年龄卡壳问题，薄熙来不能不又羡慕又妒忌。薄熙来妒忌汪洋没道理，这个错是他自己母亲的，秘书胡明与有妇之夫的上司薄一波晚鬼混几年，这个问题就解决了。<br />
<br />
汪洋生于安徽，是团派重点培养的人，他比薄熙来年轻许多，资格又比薄熙来老。汪洋在2005年调任重庆市委书记之前，是国务院副秘书长（负责国务院办公厅常务工作，正部长级）。让薄熙来仇恨汪洋的另一个主要原因是，2007年12月十七大召开后，Hu Jintao把汪洋从重庆调去广东省当书记，却把留下的山城空位给了他薄熙来。胡的这一举动引致薄熙来极度不满，他认为这是Hu Jintao故意贬低他，硬将他「外放」。因此薄熙来更恨上Hu Jintao。这让数次暗杀Hu Jintao未遂的Jiang Zemin和曾庆红看在眼里，喜在心头。<br />
<br />
Jiang Zemin认为，黄菊死了，陈良宇押了，总算有人出来接了折腾胡温的班。另外，让江无时无刻不耿耿于怀的是，胡温一直不肯接镇压FLG的这个大包袱，反而暗中调查江在镇压期间花了多少国库银子。而薄熙来在这个问题上早就是江「同一战壕里的老战友」了，这是Jiang Zemin和曾庆红力挺薄熙来进下届政治局常委会的第一大要素。挺薄并不是爱薄，而是为了保护自己不被押上法庭，这笔账江曾还是算的蛮清楚的。薄熙来也不糊涂，他说，「我不过是利用利用这两个老傻蛋而已。」<br />
<br />
● 薄熙来重点打击汪洋 <br />
<br />
十七大四中全会马上要召开，谁最担心、最着急，谁跳的最欢、最急于造势。没的说，那就是薄熙来。<br />
<br />
汪洋从2005年至2007年12月任重庆市委书记，薄熙来就让笔杆子专门攻击这一时期。9月10日，薄熙来找人在新华网法治论坛发表文章，题目很恐怖：《薄熙来打黑撕下了谁的『画皮』？》，文章直指汪洋当政时的2005年。<br />
<br />
薄熙来父子三代淫乱无比，文章却说：「走向文明，走向进步，需要道德教化，需要淳厚风气。可是，在重庆这个码头上，往往会有一些令人费解的事情发生。比如，重庆非常热衷于打『美女牌』。2005年，重庆晨报与重庆电视台生活频道联合主办了『重庆十大名片评选』活动，经过专家学者的论证评审，最终评选出十大重庆名片，依次是美女、火锅、解放碑、红岩、雄起、三峡、邹容、桥都、钓鱼城、温泉之都。美女与火锅，成为重庆的两大招牌。这似乎与先哲『食色性也』 的论断不谋而合。」<br />
<br />
薄熙来对自己的吹捧也很恐怖：「薄熙来在重庆打黑捷报频传，掀起了一股除恶锄霸的强大旋风，引来全国各地一片叫好之声。网上网下之所以好评如潮，大抵就是在于薄熙来的爱民如亲与嫉恶如仇，在于薄熙来的凛然正义与浩然正气，在于薄熙来的弘大气魄与坚强决心。薄熙来打黑，意义非同凡响，更难能可贵的是，薄熙来撕下了一张张美丽的『画皮』。」<br />
<br />
● 薄熙来的话在重庆就是「法」 <br />
<br />
重庆市委的干部透露，为了帮助薄熙来进入政治局常委会，薄亲信、现任公安局长王立军打击重庆前任市委书记们决不手软，把曾与他们亲近的干部捉去后，把在FLG修炼者身上施用的酷刑都用在这些党官身上，逼着他们乱咬前任市委书记们，然后王立军迅速扩大打击面，以达到让贺国强、汪洋等人在四中全会上对薄熙来升迁之事不敢提出异议的目地。<br />
<br />
薄熙来指示：每个部门至少要拉出一个用来「杀鸡儆猴」，而且要重判！<br />
<br />
2008年11月，原九龙坡区区长黄云被判有期徒刑13年，处罚金10万。2009年2月，市规划局原局长蒋勇因受贿1796万余元被判死缓。5月，市规划局原党组成员、市规划展览馆原馆长查红，因受贿罪被判有期徒刑10年零6个月。个个 的刑期都是由薄熙来拍板定案。<br />
<br />
今年四中全会前，薄熙来加快了脚步，继重庆市司法局长文强被双规后，9月1日给予查红、蒋勇、黄云3人行政开除处分。让他们没有任何恢复职务的可能性。市规划局局长的位子对于薄熙来非常重要，只有自己人坐上去，薄才可以想在重庆怎么规划就怎么规划。9月4日，重庆市公安局现任副局长彭长健被双规。9月8日，重庆市公安局交警总队总队长、重庆市公安交通管理局局长陈洪刚被双规。据内部人透露，副厅级干部陈洪刚在受刑不过时自杀，但未遂，只好按照专案组人员的要求，说什么承认什么。<br />
<br />
目前，小小重庆正在经历又一次红色恐怖，薄熙来为了达到个人进入政治局常委会的目地，使被殃及的党官家庭越来越多。光动用的公安就由3000人增加到了7000人，相当于重庆总警力的23%，专项行动已成立200个专案组，而且还在不断增加。为了怕走漏风声，不但专案组人员不许回家，而且薄熙来让专案组的人签下生死状，「凡走漏消息者杀无赦」。<br />
<br />
薄熙来下命令说，「专案组人员的工资和奖金与他们的工作成绩直接挂钩。完不成任务的别怪我不客气！」于是，酷刑折磨便成了家常便饭，这正是虐待狂薄熙来的最大嗜好。 <br />
<br />
● 薄熙来折腾大发了就有治了 <br />
<br />
高层一位元老说：薄一波有背后整人的习惯，没想到他儿子当个市委书记就把一个城市搞到鸡飞狗跳，要是让他掌管的面儿再大一些……不得了、不得了，那他比老毛还厉害，大家都别想活的安稳！<br />
<br />
为了达到重庆官场一色清都对自己俯首帖耳，薄熙来和亲信王立军列出抓捕黑名单，上面都是薄熙来上任以后不拍他马屁和没表示效忠他的人。薄熙来准备抓哪个，就让手下以网友身份提前在网上散布消息、制造舆论。此人双规后，酷刑之下，所「承认」的和「网友」提前「预测」的自然没有两样。<br />
<br />
由于专案组办案成员不许回家，而且专案组与专案组之间不许谈起自己所办的案子的情况和进展，所以被薄熙来弄进去的人，无法指望有人去救他。<br />
<br />
重庆的一位厅级干部家属说：我现在才体会到斯大林当政时期干部们的恐惧心理。我现在的心情也是一样，早晨看着老头子出去，晚上不知道他是否会回来。人家说文革不会再有了，可我们重庆现在比文革那会儿还恐怖。一位干部家属说：别着急，我就不相信没人出来收拾薄熙来和王立军这两个王八蛋。<br />
<br />
是啊，物极必反嘛，折腾太大发了，就等于对着自己的太阳穴开枪走火。△</div>

							
							
							
							
