中新社北京4月19日电 全国政协主席贾庆林19日指出，此次玉树抗震救灾工作任务仍然十分艰巨，民族宗教因素突出，境外敌对势力也企图对抗震救灾工作进行干扰破坏，要通过救灾和重建，进一步巩固民族团结，为灾后藏区的跨越式发展和长治久安打下坚实的基础。<br />
<br />
贾庆林是在当日主持召开中央有关部门负责人会议，传达贯彻17日中央政治局常委会议精神，对做好青海玉树抗震救灾和团结稳定工作作出部署时做上述表示的。<br />
<br />
贾庆林指出，青海玉树地震发生后，党中央、国务院对抗震救灾工作高度重视，胡锦涛总书记主持召开中央政治局常委会进行了全面部署。胡锦涛、温家宝和其他领导深入玉树震区视察灾情，指导抗震救灾工作。中央各有关部门认真贯彻中央决策部署，迅速行动起来，充分履行各自职责，发挥了重要作用。<br />
<br />
贾庆林指出，此次玉树抗震救灾工作任务仍然十分艰巨，面临着相对特殊的困难和挑战。除了高寒缺氧、部分群众生活困难、民族宗教因素突出外，境外敌对势力也企图对抗震救灾工作进行干扰破坏。在这种情况下，要坚持一手抓抗震救灾，一手抓团结稳定，以对灾区人民高度负责的精神，恪尽职守、不畏困难、连续作战，全力以赴做好各项工作，为夺取抗震救灾的全面胜利创造良好社会政治环境。<br />
<br />
贾庆林强调，要进一步发挥统一战线在抗震救灾中的积极作用，各级统战、民族、宗教部门要深入灾区各族群众、寺庙和宗教界人士中了解情况，积极与有关部门协调，组织群众、寺庙和僧尼开展救灾和自救并提供帮助，引导各地寺庙、僧尼为死难者念经祈祷活动在当地进行。对国外藏胞提出回国奔丧、捐款等要求的，中国驻外使领馆要热情相待，介绍抗震救灾和国内的真实情况，了解他们的合理要求，给予必要的帮助。<br />
<br />
贾庆林强调，要把灾后重建与贯彻落实中央关于加快青海等地经济社会发展的精神结合起来，特别是在“十二五”期间，要有针对性地加大对青海藏区的支持力度，加快工作进度，使灾区经济社会发展尽快恢复。要通过救灾和重建，彰显我们党以人为本、执政为民的执政理念，进一步密切党与各族群众的血肉联系，进一步巩固民族团结，进一步增强各族群众的祖国大家庭观念，为灾后藏区的跨越式发展和长治久安打下坚实的基础。</div>

							
							
							
							
