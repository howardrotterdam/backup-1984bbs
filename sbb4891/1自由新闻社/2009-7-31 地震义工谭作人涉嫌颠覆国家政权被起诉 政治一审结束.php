													<h2>背景材料：四川好人谭作人</h2>
						
						
																											<div id="postmessage_185616" class="t_msgfont">去年5・12地震后，我也去四川做了志愿者。本来我不想去，还写了一篇文章：《每个人都可以战斗在汶川》，意思是生活中有很多平凡的地点、平凡的事件，都需要志愿者精神。但后来，因为拍摄纪录片，我还是去了四川。就这样，我认识了不少朋友，其中最好的朋友之一，就是谭作人。<br />
<br />
&nbsp; &nbsp; 现在提笔写这些废话，文字不足以表达我的心情。窗外艳阳高照，花红草绿，谭作人不知在看守所哪间黑屋子里遭提审。他回不了家，看不到自己的妻子和女儿，晚上可能吃不饱、睡不安，还要交代各种涉嫌“垫付”的罪名。我的心情，和地震发生后幸存者的心情实在也差不多，眼瞅着亲人被埋在废墟下，你搬不动梁、掀不动钢筋，突然感受着心绞痛……作人，你可知道多少朋友在呼唤你的名字、他们甚至到了关押你的温江看守所门口――当然是没有人接待的；此刻，谁能救得了你呢？<br />
<br />
&nbsp; &nbsp; 作人是四川知名的环保工作者、《文化人》刊物主编，他的文章网上都能看到――假如你会翻墙的话；根据他的文章，你能判断出他是个什么样的人：古道热肠，侠肝义胆。德国戏剧家布莱希特写过一出戏叫《四川好人》，说的是三个活神仙到了四川，想找心口如一的好人，当然是没有找到的。里面还有一句台词，叫做“丢尽了四川的脸”。布莱希特死得早，不然我就请他告诉他的神仙们，去找谭作人吧，找到他，你就能找到甚多的四川好人。<br />
<br />
&nbsp; &nbsp; 作人的好，在于身体力行，这是我的第一个印象。例如，四川人对家乡的认同感在全国是很突出的；吃川菜、摆龙门阵，这就不用说了。可是作人爱四川，是把他的乡土感情融入到作为公民的责任里。记得我们一起去看地震带，他一路讲起彭州石化选址的不当，真是雄辩滔滔。他对四川风物、文化和历史的渊博知识，也让我叹为观止。正因为如此，他会认真地发起“和平保城”的公民行动，在那个“我们不示威、我们示弱”的公民建议书里，他如实署上自己的姓名、身份证号和联系地址。他不是那种既考虑自由表达又“躲猫猫”的人，这不是一般的勇气，而是证明和争取。他证明着，这是我的权利、我的承诺、我的责任；这也是我的乡土、我的国家、我的人民。用他妻子的话来说“他是那种爱国爱得吓死人的人”。作人的爱，岂止是践行，而且是牺牲；然而在这物欲横流的俗世，牺牲早已不是中国人日常生活中的常用词。<br />
<br />
&nbsp; &nbsp; 记得我们一起去北川，那时川震百日祭刚过去两天，作人在网上发出《龙门山，请为北川孩子们作证》。我问他说，现在已经抓了好几个人了，你还写？他说，即使提笔就死，也要写出来；于是有了好友崔卫平文章中引用的那一段他在我镜头前所说的话。独坐震后北川河畔，作人遥望孤城，那画面让我想起杜甫诗句：“国破山河在，城春草木深”。离开时天色将黑，似有亡灵纷至沓来。奔走一下午，我们都精疲力竭，但为抄近路，依然决定爬山出城；作人带着自己的行李，一路帮我背着摄像机的包。那路不好走，我已经斯文不得，四肢着地爬上高坡。作人一声不吭，埋头走在我们前面。他的做派，让我想起他所说的青年时代，那时他当知青，还是生产队长；作人依然有那“以天下为己任”的情怀。<br />
<br />
&nbsp; &nbsp; 也是在那一次，我们在绵阳市宏升建设监理公司，看到有关北川中学教学楼施工质量的会议记录，我把它抄录在这里，供懂得监理的专家分析：<br />
<br />
&nbsp; &nbsp; 绵阳市宏N建设监理有限公司会议记录<br />
<br />
&nbsp; &nbsp; 时间：2006年3月31日<br />
<br />
&nbsp; &nbsp; 地点：设计所办公室<br />
<br />
&nbsp; &nbsp; 主持：王卓伟（所长）<br />
<br />
&nbsp; &nbsp; 参加人员：监理公司在建工程项目监理员<br />
<br />
&nbsp; &nbsp; 主题：在大检查中存在的问题<br />
<br />
&nbsp; &nbsp; 以下为涉及到北川中学部分：<br />
<br />
&nbsp; &nbsp; 周潇龙：北一中<br />
<br />
&nbsp; &nbsp; 施工方安全安全文明施工相当差，施工方组织验收的人员不到场。二楼全面防护没做，楼梯通道口没有做防护。施工方资料不齐全，特别安全交底签字不全，人员缺岗比较严重。<br />
<br />
&nbsp; &nbsp; 质量施工质量比较差。配给比，卵石含泥比较中（这里的“中”可能是写错了，应该是“重”――晓明注）。<br />
<br />
&nbsp; &nbsp; 监理现场管理经验不足，特别是安全知识不是很懂。资料、图纸含章，我们监理上没有正规表格，工地测绘，管理人员缺岗比较严重，工程进度没有评价，没有调整。<br />
<br />
&nbsp; &nbsp; 基础验收现施工二楼搂道，基础没有验收，检查中要求基础验收后方可进入主体施工。见证资料没有表格。<br />
<br />
&nbsp; &nbsp; 王卓伟作了总结：<br />
<br />
&nbsp; &nbsp; 1．（略）<br />
<br />
&nbsp; &nbsp; 2．（略）<br />
<br />
&nbsp; &nbsp; 3．（略）<br />
<br />
&nbsp; &nbsp; 4．检查中所发现质量问题<br />
<br />
&nbsp; &nbsp; （1）（略）<br />
<br />
&nbsp; &nbsp; （2）北一中，施工缝留直（置――晓明注）错误，留在跨中， 地梁必须支模。监理日记、目标、评价，不吻合。旁站记录偏少，必须真实。<br />
<br />
&nbsp; &nbsp; 北川绵阳市宏N建设监理有限公司的办公楼并没有倒塌，但是，这个会议记录中提到的王卓伟和周潇龙两位先生，不知有没有逃过地震一劫。无论如何，这里的描述并不复杂，不难得到解释。我唯一不太明白的是，北川中学新教学楼建成于2003年，为什么会在2006年检查施工质量？难道是文档检查吗？北川中学依然存在、北川依然有工程监理，希望知情人能够做出专业上的结论。下面我继续说说我和作人交往的经历。<br />
<br />
&nbsp; &nbsp; 去年8月底我离开四川，回到学校上课；时不时也收到作人的来信，其中有两次特别难忘。一个是在去年9月，他第二十次进入灾区，晚上接到北川中学遇难学生家长陆世华的信，这位学生家长，将自己的写公开信的经历分为18段手机短信发到作人手机上。其中写道：<br />
<br />
&nbsp; &nbsp; 我陆世华，高一、二班陆芳的父亲，十六年前老婆为生此女更丧身，十六年独身一人将全部精力心血寄托在她身上，她也很优秀，无论是品德还是成绩。 “5・12”她去了，去得让人承受不来。实观北一中现状，我认为是天灾是部分因素，劣质的建筑造才是成重大伤亡的主要原因。放眼北一中所有建筑，为什么主教学楼会被夷为平地？而其它建筑无妨？被称为危房的无妨？如果它质量在(再)好一点，说不定可多活一个年幼的生命，少一个家庭无数人的痛苦。上千个年幼的生命，被压得面目全非，残缺不全，惨不忍睹，伤者如何？残者何为？为了给孩子们一个交待，为了这种悲剧少发生，不发生，我有几点提议：一、七月一日七七请全体家长到北一中现场，公祭死难者；二、要求相关部门人我们一个合理的说法，如真有人为因素，更求依法严厉查处；三、对死难者家属如何安抚？对伤残者如何安排？如有赞同者请与我联系。电话：&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; <br />
<br />
倡议人陆世华二零零捌年五月二十日<br />
<br />
 <br />
<br />
&nbsp; &nbsp; 就因为这封信，涉嫌“聚众闹事”，陆世华被警察从片口老家带到绵阳市派出所，关了18天。可他依然不甘心，还要问个究竟，七月十二日，他再次给各级党委和政府写信：<br />
<br />
&nbsp; &nbsp; 一九八四年六月三十日在梓潼县召开了绵阳地区规划评审会议，就北川县城迁治城提出评审。一九八四年七月三十日北川县政府以七十八号文件上报行署，省建委以八六・三十五号文件批复，为什么迟迟未动，近几年还在大建？“5・12”县城死亡、失踪二万多人，制（致）残数百，经济损失数亿，上万人无家可归，这是哪级政府在犯罪？二、北川县地处龙门山脉断裂带中段，历史地震发生频繁，早在一九七七年四月北川县正式列为地震《八烈度设防》。绵阳市仅北川列入省抗震办设防重点县。作为九八年才新建的教学楼，为什么瞬间夷为平地，而相邻七十年代建筑被你们称为危房的未垮，这是为什么？三、北一中垮塌的教学楼是优质的吗？是合格的吗？地震发生前（后）几天温总理不是也公开说过要查建筑质量吗？<br />
<br />
&nbsp; &nbsp; 陆世华被抓、被打压，但是他就是要个说法，他要追究豆腐渣工程。因为看到作人《龙门山，请为孩子们作证》一文，他觉得作人是好人，所以不停地给他发短信，重复那让他肝肠寸断的天问：我把健康鲜活的女儿送进学校，是让她去送死的吗？<br />
<br />
&nbsp; &nbsp; 作人看完短信，又接着听陆世华在电话里的诉说，一直到凌晨三点。秋雨潇潇，没有答案，电话两头都是做父亲者为孩子流血的心。<br />
<br />
&nbsp; &nbsp; 今年春节，我也收到作人的信，这次是讲的是王学兵的故事。王学兵的家在大地震的第一爆发点，5・12之后，他的父亲、母亲被吞没，王学兵没有找到家里的任何遗物。他放养的羊群先因地震失去多半，劫后余生者又有几只被掩埋于泥石流。这一切苦难已足够惨烈，到了年底，王学兵惨遭车祸。这一次，他花光了自己的积蓄、父母身亡得到的抚恤金；“躺在床上的刚强汉子，想起年关将至，却无法筹集到2万元手术及治疗费用，止不住泪珠滚滚！”作人写道：<br />
<br />
&nbsp; &nbsp; ……特大灾难啊，你究竟要把人击倒多少次？六十周岁的人民共和国啊，您的社会救助体系，是否还有所不足？有待改进？<br />
<br />
&nbsp; &nbsp; 在此，笔者吁请海内外爱心人士伸出援手，救助地震灾民王学兵，让他能够施行手术治疗，别让冷漠和另外一种“次生灾害”再次害人，再次危及死里逃生的地震灾民的健康和生命！<br />
<br />
&nbsp; &nbsp; 中国将会感谢您的奉献和爱心！<br />
<br />
&nbsp; &nbsp; 谭作人一介书生，为王学兵呼吁捐款，却用了“中国将会感谢”这样的字句，你说他把自己当成了什么？中国的代表吗？又或者，他知道，时隔8个月，国人爱心高潮已去，他更多地吁请海外爱心人士？不管怎样，这就是谭作人，一个面对他人的痛苦无法转身的人，一个“爱国爱得吓死人的人”。<br />
<br />
&nbsp; &nbsp; 我不认识王学兵，作人的信让我坐立不安。我赶快将他的信转发给了朋友们，也给王学兵寄了钱去。两个月后，我接到王学兵的电话。他说，因为春节，他一直没有收到钱。现在收到了，他的身体好多了。他说：你以后过来耍啊！这一个“耍”字，乡音恳切，又轮到我“泪水滚滚”。<br />
<br />
&nbsp; &nbsp; 急人之难、助人为乐，作人就是这样做人的。所以，当我收到短信说“作人被它们抓了”，我觉得这不可能；这像一个低级错误，更像一个愚人节的笑料。你把这样的人抓起来，还说他“涉嫌垫付国家政权”，没有人信的。作人为这个国家的政权，实在做了太多的好人好事，堪称活雷锋。和雷锋只有一点不同，就是作人秉承公民的独立思考和信念，并且勇于践行。他肯定得罪了一心要在彭州搞石化的人、得罪了绝不要查豆腐渣的人，这些黑材料，早有人给他记着。并且，在他被捉之前，已有若干预兆：先是电脑被盗，然后发现在局子里；再有一天人在途中，接到孩子的电话说：还差500元钱；原来狗被捅了刀子。作人心说：今天捅狗，明天捅人。果不其然，不似刀客，胜似刀客，这一天如期而至。<br />
<br />
&nbsp; &nbsp; 据说我英勇的人民警察在作人家从早上10点抄到下午6点，该拿的都拿了。又听说他的一条新增罪名是发起全球华人6・4 献血，谭作人啊谭作人，早已过了血气方刚的年龄，你依然不改“我以我血献轩辕”的初衷。你明明知道，无数无数的中国人，在某些时候，也包括我自己，为了安宁和苟活，在巨大的灾难面前背过脸去。只有你这样的少数，如鲁迅所说“真的猛士，敢于直面惨淡的人生，敢于正视淋漓的鲜血”。在北川中学废墟上，你捧起染着大片孩子血迹的书本泪流满面，这一年来，你的汗和泪还流得不够吗？你居然要去掀起这二十年来有关方面讳莫如深的日历，要刺破你的脉搏，把你还在发烧的血抛洒在这多难的国土上！中国九百六十万平方公里啊，你那一腔血，倒出来有没有两茶杯？可够浇一棵枯草？你真应了你妻子的一句话：“爱国爱得吓死人”；这一回，居然就把人吓着了。<br />
<br />
&nbsp; &nbsp; 为了这句话，我向作人的爱妻深深致敬；再没有哪句话，能如此生动地形容作人那“做人、特别困难地做中国人”的状态。我写下以上这些拉拉杂杂的事，与其说留给作人将来看，不如说是写给看管谭作人的警察同志――希望你们善待我作人兄弟，你要饿了他，作践了他，你就像布莱希特在《四川好人》的戏剧中所说：丢尽了四川的脸。<br />
<br />
