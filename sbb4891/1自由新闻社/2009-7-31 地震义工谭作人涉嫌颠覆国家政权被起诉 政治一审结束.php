以上是起诉书全文。文本中，颠覆耶？诽谤耶？检察院自己都拎不清。说诽谤吧，诽谤对象有<strong>政府</strong>，还有<strong>“我党”</strong>。颠覆政权吗？证据呢？作为目击者记述当年广场情景、纪念死者、献血……<br />
<br />
对了，说起献血，想到一件事。今年5月35日（好像还有前后几天），天府广场献血点，警车取代了献血车。过了一些日子，本地电视新闻有个内容，大意是：血库告急，呼吁市民积极献血。<br />
<br />
根据这份起诉书，看得出来，前面所控纪念5月35日是虚，不便说出的真实目的是对谭作人就地震暴露的豆腐渣工程、就彭州石化项目进行调查、接受采访进行清算。<br />
