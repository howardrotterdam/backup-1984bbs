													<h2>地震义工谭作人涉嫌颠覆国家政权被起诉 政治一审结束</h2>
						
						
																											<div id="postmessage_185604" class="t_msgfont">2009-07-31 11:19:45 <br />
原文地址: <a href="http://blog.sina.com.cn/u/5a0f79650100ehsz" target="_blank">http://blog.sina.com.cn/u/5a0f79650100ehsz</a> <br />
<br />
肖雪慧<br />
<br />
昨日，从王庆华女士那里获悉，她的丈夫谭作人已被成都市检察院正式起诉，开庭时间是8月12日上午9点半。谭作人将被起诉，她是头一天（7月29日）收到谭作人寄自看守所的信后才知道的。信中是一纸通知，告知他将被正式提起起诉，可委托家属请律师。这份6月13日的“通知”，谭作人6月29日才看到。到王庆华手里，又相隔一个月。而检察院于7月17日就已经正式提起起诉。好在，王庆华女士为保障谭作人的权益提前着手相关事宜的联系，聘请了浦志强、夏霖两位律师。如果待收到姗姗来迟的通知再作打算，根本来不及请律师。这中间，有关部门的小动作不说也罢。检察院究竟把什么罪名加诸谭作人，这才是最重要的。王庆华给了我一份起诉书副本复印件。照录出来，奇文共赏。<br />
<br />
<strong>四川省成都市人民检察院起诉书</strong><br />
<br />
成检刑――诉字（2009）183号<br />
<br />
被告人谭作人，男，1954年5月15日出生，身份证号51010219540515611X，汉族，中专文化，住成都市武侯区望江路29号桃林村6栋6号，2009年3月28日被依法执行刑事拘留，2009年4月29日因涉嫌煽动颠覆国家政权罪经成都市人民检察院批准，次日由成都市公安局依法执行逮捕。<br />
<br />
该案由成都市公安经侦查终结，以被告人谭作人涉嫌煽动颠覆国家政权罪，于2009年6月29日移送本院审查起诉。本院受理后，已告知被告人有权委托辩护人，依法询问了被告人，审查了全部案件材料。<br />
<br />
经依法审查查明：被告人谭作人对党中央处理“XX事件”方法和定性不满，多年来以各种方式从事所谓纪念“June Fourth”的活动。2007年5月27日，谭作人炮制了一篇题为“1989：见证最后的美丽――一个目击者的广场日记”文章，并将该文通过互联网发布在境外“自由圣火”等网站，该文主要内容为对党中央处理“XX事件”进行歪曲描述和诽谤。文章刊登不久，境外<strong>敌对分子王丹</strong>利用电子邮箱与其主动联系，并多次向其投发关于“XX”的宣传资料。2008年6月4日，被告人谭作人与他人在我市天府广场义务献血点以献血的方式纪念“June Fourth”，期间还接受了境外<strong>敌对媒体“希望之声”</strong>的电话采访。2008年11月以来，王丹多次向其投发了纪念“XX”进行所谓二十周年活动的相关资料。2009年2月10日被告人谭作人向王丹发了一分《XX20周年纪念活动建议》电子邮件，建议在今年“XX”期间实施所谓的“XX全球华人义务献血活动”，以纪念“XX”二十年。2008年“5.12地震”发生后，被告人谭作人多次接受境外媒体采访，发表了大量严重诋毁我党和政府形象的言论。2009年3月27日被告人谭作人被公安机关挡获归案。<br />
<br />
认定上述事实的证据如下：辨认笔录、挡获经过、鉴定书、证人证言、被告人供述等。<br />
<br />
本院认为，被告人谭作人为了达到颠覆国家政权、推翻社会主义制度的目的而无中生有，捏造消息，散布有损于国家政权和社会主义制度的言论，以损害国家政权和社会主义制度在人民心目中的形象。其行为已触犯《中华人民共和国刑法》第一百零五条之规定，犯罪事实清楚，证据确实、充分。应当以煽动颠覆国家政权罪追究被告人谭作人刑事责任。根据《中华人民共和国刑事诉讼法》第一百四十一条之规定，提起公诉，请依法判处。<br />
