																											<div id="postmessage_348586" class="t_msgfont">歼十是不是国产飞机？公务员考试考哭女大学生<br />
<br />
<img src="http://images2.china.com/mili/zh_cn/important/64/20091130/2009113010263584019900.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
<br />
1.5万个岗位，约100万人争夺，最热职位4000多人竞争，2010年国家公务员考试竞争激烈程度又创了纪录。昨天，全国各地百万考生同时走进笔试考场，接受同一场检验。<br />
<br />
 考生们普遍感觉，国家公务员现在越来越难考了，除了僧多粥少，淘汰率达到惊人的98%外，考题的难度也提升了，要想拿高分，就要成为博学专家，武器、科技、能源、文化、经济、政治、人文管理方面均要有涉猎。<br />
<br />
&nbsp; &nbsp; 多名考生感觉行测较难<br />
<br />
 当行政能力测试（简称行测）考试结束后，考生小李一出考场，就冲着等待在考场外的男朋友发问:“АК―47突击步枪是全自动还是半自动？”女孩叹着气说，行测中有很多知识都不知道，只能瞎蒙。<br />
<br />
 记者发现，今年行政能力测试的考试让不少考生感到束手无策。比如，“最大咸水湖”、“АК-47突击步枪”、“SU-34”“核潜艇”都成了考试内容。“这些东西真是很难复习到，”小李告诉记者说，“都是百科知识，只有平时积累多才能得心应手。”国考结束后，一位网友在“公务员论坛”上表示，“资料分析根本都没时间做，25道有20道是蒙的，早知道就不复习那么多了，复习还不如裸考。”<br />
<br />
 据悉，今次行测考题总量仍为140题，但各模块在试卷中的顺序有了调整，语言理解是试卷第一部分，而原本放在卷首的常识题被排在了最后。<br />
<br />
&nbsp; &nbsp; 申论考试题目相对容易<br />
<br />
&nbsp; &nbsp; 昨天16时30分，国家公务员申论考试结束。很多考生长出了一口气，他们告诉记者，与上午的行测相比，申论考试容易了许多。<br />
<br />
 据了解，2010年国家公务员考试申论考察的主题是“海洋开发与保护问题”。考生小周表示，题目相对比较容易做，考完后甚至留有半小时时间检查。也有考生向记者抱怨，这个题目太工科了，对文科学生来说不太公平。而另外一些考生就说，题目有点旧，好像老生常谈，“如果是结合时事来谈，更能考验能力”。<br />
<br />
&nbsp; &nbsp; 不少考生认为，申论的题目越容易，成绩就越难拉开档次，所以可能还是要更多地看上午行测的分数。<br />
<br />
 应届生为市考提前“练兵”<br />
<br />
 “炮灰”是考生议论的一个焦点问题。考场外，考生们聚集在一起对比自己的职位录取比是多么的恐怖。这个说自己报了100:1的职位，那个说自己差一点1000:1。一位考生最后说，国考注定是“炮灰”，当作是练笔，市考好好报，就报崇明，看看还有没有人竞争。<br />
<br />
 在日企工作的厉盈盈告诉记者，这是她第二次参加国考，考场印象最深的就是考生中应届生比例大增。“去年考试时，考场上有很多年纪较大的考生，而今年，基本上都是大学生模样。”有这个感觉的不止是历盈盈一个人，不少考生都发现今年学生比例增加了不少。“这下子更难了，全是学生在考，不知道会把录取线抬升多少。”一位出场的学生抱怨说。<br />
