													<h2>我认识的薄一波的孙子……</h2>
						
						
																											<div id="postmessage_90613" class="t_msgfont">当然啦，我认识不到薄瓜瓜，他太年轻了。我曾经去Google搜了很久，想知道，我认识的那个薄一波的孙子究竟是他哪个儿子生的，他究竟大名为何，也没搞定，反正应该是薄熙来的哥哥吧，大名好像是几个特别难写难认的字，对小学生来说。<br />
<br />
我跟这个“薄一波的孙子”――不好意思，大名忘记了――是小学同学，中古友谊小学，在北京三里河一带。那边挨着钓鱼台国宾馆和物资部、一机部等几个国家部委，貌似藏龙卧虎之地啊。但是80年代上小学的路上仍然很臭，有臭水沟，有成排的垃圾桶，很窄的路……<br />
<br />
这个“薄一波的孙子”是小学时唯一的阴影，很霸道，很野蛮，长了个方形脑袋，见谁打谁，那真是……挺吓人的。同学们都躲着他，尽量少跟他交往，因为他脾气真的很坏，好像揍人是他唯一的乐趣。而且，在老师们中他也有独特的气场，那就是，没有老师愿意管他，他爱干嘛干嘛。他可以随意不上课，他可以在上课途中大模大样走出教室，随便叫喊，上体育课时，他可以参加，可以不参加，但所有人就当他不存在一样……但是你当他不存在，他就会偶尔存在一下，那就是给做游戏的同学捣乱，吵架，然后揍你……所以呢，他一旦出现在和谐的做游戏的同学们中间，同学们就会瞬间作鸟兽散，而且个个表情貌似啥事儿也没发生似的。当然，时不时也有同学被打轻伤，他也时不时挂点儿彩，时间长了，同学们也都不跟他一般见识，惹不起躲得起。<br />
<br />
有一次，我在校门口等家长来接，这个“薄一波的孙子”也在等（应该是没有汽车接送，因为学校离他家实在很近），我带了一条红色纱巾，长长的，吸引了他的注意，于是要抢我的纱巾，纱巾在我脖子上，他一抢，正好勒住我的脖子，这一幕被我强悍的奶奶看见了，上前制止，而“薄一波的孙子”嘴里骂骂咧咧，手上挥着拳头，并往地上吐口水来回应。两米远处，是他家保姆陪着笑，道着歉，依然没有人管他。<br />
<br />
顺便说一句，他家保姆有好几个，其中一个在他家工作时间最长的老保姆，是我家保姆的妈妈。安徽人。<br />
<br />
现在想起来，这样的孩子，其实很可怜的。他如果就这个样子长大，没有人留意到他的性格和精神健康的话，我自认为他会是个人格不那么健全的人，会很痛苦吧。<br />
<br />
后来说起童年，我爷爷说，薄一波的大儿子，大女儿什么的，好象是农村的老婆生的，后来娶了大家众所周知死在文革中那个老婆。生了薄熙来几个后面的孩子。而且据说，薄熙来的老妈很漂亮，薄熙来真的很花心。<br />
<br />
<br />
<br />
PS：刚才旁边有童鞋说景山学校什么的，景山学校确实是高干子弟学校。我前男友曾在那里上初中，对那里保佑一辈子的童年幻想。并且，他的初恋女友也是他景山学校的同学，这个女生的后爸是当时的公安部部长叫陈什么，这个故事蛮有意思，我小8一下哈……<br />
<br />
陈部长的老伴好像和这个女孩的妈妈是亲戚，管他老伴叫“姨”，陈部长老伴死后，把这个女孩和她妈都托付给陈部长照顾，照顾着就不知不觉照顾成后老伴儿了，当然，很有可能是老伴儿临死前希望这样安排的。所以女孩的妈比陈部长小很多。据说，陈部长对这母女俩都很不错的。<br />
<br />
后来这个女孩上了对外经贸大学，大二就出国了，美国。<br />
