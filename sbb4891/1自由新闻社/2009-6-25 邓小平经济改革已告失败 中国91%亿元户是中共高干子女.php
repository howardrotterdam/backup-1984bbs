There are now more millionaires in China than in Britain, according to the latest authoritative survey of the super-rich. The Capgemini/Merrill Lynch World Wealth Report reveals that by the end of last year there were 362,000 &quot;high net worth&quot; people in the UK, down from 491,000 in 2007 and 2,000 fewer than the researchers say live in China. &quot;High net worth&quot; is defined as those who have &quot;investable assets&quot; exceeding $1m (&pound;607m) that is excluding their homes, collectables and consumer goods.<br />
<br />
<br />
Across the world, the very wealthy have been hit hard by the recession and declines in property values, stock markets, hedge funds and salaries and bonuses in the financial sector. The synchronised decline in asset values has left no hiding place for wealthy investors. The world population of high net worth individuals in 2008 was 8.6 million C down 14.9 per cent on the previous year, and the value of their collective wealth declined by 19.5 per cent, to $32.8 trillion. <br />
<br />
The position of &quot;ultra high net worth individuals&quot; C those with liquid assets of $30m or more C is worse. Their number has declined by a quarter to about 80,000, and they are 23.9 per cent poorer than they were. This group commands about $10trn in assets. Thus, some 0.001 per cent of the population of the world C equivalent to the population of Hemel Hempstead C still owns 10 per cent of all the wealth on the planet. Merrill Lynch put the losses down to risky investments, or &quot;that group's partiality for more aggressive products, which tend to deliver greater-than-average returns in good times, but delivered hefty losses in 2008&quot;. <br />
<br />
In Germany the number of high net worth individuals fell by only 2.7 per cent, as they pursued a more conservative investment policy than their counterparts in Hong Kong, for example, where the number of millionaires dropped by 61.3 per cent. Brazil overtook Australia and Spain in the wealthy stakes to rank 10th in the world C cementing its reputation as one of the world's most unequal societies. <br />
<br />
The largest number of high net worth individuals can still be found in the US C 2.5 million, with Japan second on 1.4 million. The report adds that the tribulations of the mega-rich have hit the markets for fine art, private jets and yachts especially badly. They have instead sought refuge in cash and US Treasury securities.</div>

							
							
							
							
