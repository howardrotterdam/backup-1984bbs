													<h2>一些技术性的说明</h2>
						
						
																											<div id="postmessage_137344" class="t_msgfont">蔡继明委员说：“中国权威部门的一份报告显示，0.4%的人掌握了70%的财富，财富集中度高于美国。”<br />
<br />
首先，我们先明确，蔡委员谈论的是“0.4%的人掌握了70%的财富“，财富是存量概念，而收入是流量概念，二者当然是不同的，对其区别，我们很清楚。<br />
<br />
其次，如果0.4%的人掌握了70%的收入，那么相应的基尼系数大概是多少呢？<br />
<br />
所谓基尼系数，简单说明如下<br />
<br />
<img src="http://i44.tinypic.com/4hz1pc.png" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
将全社会所有人口的收入，从低到高排序，并将此数据绘制成洛仑茨曲线（Lorenz Curve） ，该曲线横轴，是累计人口百分比，纵轴是累计收入百分比。当收入分配绝对平均时，收入最低的1%人口拥有1%的收入，收入最低的2%的人口拥有2%的收入，....。因此收入分配绝对平均时的洛仑茨曲线是图中的绿色直线。实际收入分配曲线（红色），必在该绿色直线的下方（因为是按收入从低到高排序）。<br />
<br />
图中的实际收入分配曲线和收入分配绝对平等曲线之间的面积为A，实际收入分配曲线右下方的面积为B。并以A除以A+B的商称为“基尼系数”，该值用于衡量社会分配的平均程度。<br />
<br />
如果0.4%的人掌握了70%的收入，并且假设，这0.4%的人口，是平均分配这70%的收入，而99.6%的人，则平均分配剩余的30%的收入。那么对应的基尼系数是0.696。<br />
<br />
<img src="http://i42.tinypic.com/1zcg5ue.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
应说明的是，如果组内的收入分配不均等（无论在穷人组内，或者在富人组内），基尼系数会更大。所以，这个0.696是下限。<br />
<br />
从已有的研究看，多数研究的结论是，目前中国的基尼系数，大约在0.4-0.5之间。<br />
<br />
因此，蔡委员给出的这组数字后所对应的基尼系数，与目前学界认知，相去甚大，且出处不明，应予质疑。<br />
