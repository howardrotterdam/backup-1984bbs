													<h2>邓小平经济改革已告失败 中国91%亿元户是中共高干子女</h2>
						
						
																											<div id="postmessage_136151" class="t_msgfont"><a href="http://finance.ifeng.com/news/hgjj/20090625/839527.shtml" target="_blank">http://finance.ifeng.com/news/hgjj/20090625/839527.shtml</a><br />
2009-06-25 04:02:26　来源: 时代周报(广州)<br />
本报记者 韩洪刚<br />
<br />
本报记者 韩洪刚<br />
<br />
中国在社会财富增长加速的同时，出现了财富向少数人手中集中的倾向。在日前召开的政协十一届常委会上，中国财富的“集中度”正在受到政协常委和委员的热切关注。<br />
<br />
蔡继明委员说：“中国权威部门的一份报告显示，0.4%的人掌握了70%的财富，财富集中度高于美国。”<br />
<br />
“有钱人”是谁<br />
<br />
查阅了几组权威数据显示，中国财富的确在以全球最快的速度流入富人钱包。<br />
<br />
2007年5月，波士顿咨询公司发布的《2006全球财富报告》指出，在中国，0.4%的家庭占有70%的国民财富；而在日本、澳大利亚等成熟市场，一般是5%的家庭控制国家50%―60%的财富。<br />
<br />
2008年8月，美林集团与欧洲最大的咨询公司凯捷咨询在北京联合发布的《2008亚太财富报告》指出，截至2007年底，中国共有41.5万位个人资产超过100万美元的富人。从财富平均拥有量来看，中国百万富翁人均掌握资产达510万美元，高于340万美元的亚太地区平均值。<br />
<br />
专家指出，目前中国已经成为全球财富最为高度集中的国家。中国财富向富人的集中度正在以年均12.3%的加速度在增长，是全球平均增速的2倍。<br />
<br />
而中国国内的一份报告，则清晰地表明了社会财富集中在什么人手上。据国务院研究室、中央党校研究室、中宣部研究室、中国社科院等部门一份联合调查报告的数据，截至2006年3月底，中国内地私人拥有财产(不含在境外、外国的财产)超过5000万元以上的有27310人，超过1亿元以上的有3220人。在超过1亿元以上的富豪当中，有2932人是高干子女。他们占据了亿元户的91%，拥有资产20450余亿元。而考证其资产来源，主要是依靠家庭背景的权力资本。<br />
<br />
不患寡而患不公<br />
<br />
3.8亿元一套的别墅，1.4亿元一架的游艇，4300万元一辆的汽车，1800万元一只的腕表，天价奢侈品纷纷登场。<br />
<br />
6月10日，《福布斯》中文版首次发布的“中国奢侈品市场调查”显示，当全球陷入经济衰退之际，中国已经成为国际奢侈品品牌最看好的市场。6月22日，“中国第一豪宅”汤臣一品一天内卖出了5套，均价每平方米11万元。这并非个案。5月底，深圳华侨城推出的7套别墅一天全卖光，均价每平方米12万元。<br />
<br />
在蔡继明看来，正是这种大部分社会财富集中在少数人手中的格局，导致了中国的畸形消费现象。<br />
<br />
目前，畸高的房价成为财富过度集中的一种典型体现。对中国的中产阶级有着深入研究的南京大学社会学院院长周晓虹教授表示：“高房价成为中国中产阶级成长的主要障碍。在中国，收入差距的拉大不仅伤害着底层的人民，也开始伤害中产阶级。”<br />
<br />
可以被划为中产阶级之列的副厅级干部、广东省政府发展研究中心副主任李惠武在6月20日表示，“以目前的广州楼价，有七八成广州市民买不起房。”他还以自己为例说明，“广州很多房子价格超过2万元/平方米了。我是副厅级职称，每月工资8000多元，两个月工资也买不起一平方米的房子。”<br />
<br />
据资料显示，如今出现了中产塌陷的M型社会倾向，精英循环与精英结盟成为一种定型，其他阶层的发展空间不断遭到侵占。1994到2004年10年间，中小企业和个体户生存环境恶化，全国有770万家个体户消失；相反，垄断行业占有了全民资源，垄断企业并无企业精神，它们只是权力的衍生品。<br />
<br />
难解权力魔咒<br />
<br />
《法国大革命讲稿》作者、19世纪英国知识界和政治生活中最有影响的人物之一阿克顿爵士说过一句被后人广为引用的话：“权力导致腐败，绝对权力导致绝对腐败。”当下的中国，改革的聚焦点，仍然是如何对过度集中的权力进行限制。<br />
<br />
“倘若不从根本上解决权力过度集中的问题，就无法阻断这样的循环链条：社会不公平带来的一系列社会问题，必然地阻碍科教和经济发展，经济发展放缓，最后痛苦的承受者必然是底层百姓；老百姓的痛苦又会转化为对利益集团的痛恨，从而给社会的动荡埋下了伏笔。”一位分析人士对时代周报记者表示。<br />
<br />
周晓虹说：“改革30年来，能够肯定的是，经济的总量确实是发生了翻天覆地的变化。但是对资源的分配，我们从来没有看到向好的方向发展”。<br />
<br />
<strong>邓小平曾言，如果改革造成了两极分化，那改革就是失败的。</strong>目前，解决贫富两极分化，在周晓虹看来困难重重：“讲白了就是要对权力进行限制，但是在目前格局下，没有办法做到这一点。而且，30年中间我们看到，经济总量在越来越增加，权力对经济的掌控是越来越高，而不是越来越低。格局不改变的话，问题会越来越严重，而不是越来越轻。”</div>

							
							
							
							
