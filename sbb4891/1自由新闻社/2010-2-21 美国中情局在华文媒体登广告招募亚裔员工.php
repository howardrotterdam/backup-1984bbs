																											<div id="postmessage_486797" class="t_msgfont">为能广招各族裔优秀人才，美国中央情报局（CIA）摆脱过去广告风格，于中国农历新年期间在南加州华文媒体大登广告，招募亚裔新血。<br />
<br />
　　CIA还首度以中文“新年快乐”向华裔民众拜年，广告画面上还配有一只威风凛凛的老虎，中国味浓厚，备受瞩目。<br />
<br />
　　连登3日全版广告<br />
<br />
　　CIA在民众心中一直充满神秘，最近两三年着重招募各族裔人才，华裔也不例外。<br />
<br />
　　今年春节更大手笔在洛杉矶《世界日报》上连登3日全版广告，就是为寻找亚裔“千里马”。<br />
<br />
　　CIA发言人保拉&#12539;怀斯指出，CIA打算在2012年将少数族裔员工的比例提升至22%~30%，并提供90个工作职位，招揽各少数族裔第一、二代美国公民。这些人员必须是“充满爱国心的优秀人员，将在美国本土工作或派驻海外”。<br />
<br />
　　怀斯说，各族裔人才招募有助提升职员的整体素质。据悉，CIA平常也对职员进行16国语言能力的培训，目的是为了解读密码以及了解各国政治、经济与文化，达到 “与世界接轨”的目的。<br />
<br />
　　至于在华裔社区的招募情况以及华裔所占的比例，CIA均以“国家安全”为由，拒绝透露。<br />
<br />
　　在很多人的印象中，CIA的工作相当刻板。许多人认为，中情局职员必须深入虎穴，冒着枪林弹雨，十分危险。事实上，除收集情报外，CIA也招募科学家、数据分析人员、外语教师、心理专家、办公室操作员以及管理人才等，多数人的工作地点都在华盛顿。<br />
<br />
　　“求才若渴”门槛很高<br />
<br />
　　 CIA年薪依照职位、学历背景、工作经验、第二语言能力等条件，从3.5万美元到12万美元不等。<br />
<br />
　　申请者需登录CIA官方网站进行申请并上传个人履历，可同时应征4个职位。符合条件者，45天给予回复。据悉，电子工程师的年薪介于5万美元至8.7 万美元之间；而负责教导职员的外语教师，其年薪最低也有5万美元，若肯被派往他国从事语言翻译与谈判沟通工作，其年薪可高达11万美元，比工程师还多。<br />
<br />
　　虽然美国目前的经济很不景气，但CIA求才广告却从未间断。由于CIA的门槛很高，符合录取条件者不多。<br />
<br />
　　申请者需年满18岁，大学GPA（平均成绩点数）在 3.0以上，还得通过多关面试、体能和心理测试、性向测验，以及语言测试，入选后还得接受魔鬼训练。<br />
<br />
