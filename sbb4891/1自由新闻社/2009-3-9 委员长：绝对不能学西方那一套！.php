													<h2>吴邦国：我国不搞多党轮流执政</h2>
						
						
																											<div id="postmessage_48936" class="t_msgfont">3月9日9时，十一届全国人大二次会议在人民大会堂举行第二次全体会议，听取全国人大常委会委员长吴邦国作全国人大常委会工作报告。<br />
　　[吴邦国]五、切实加强自身建设<br />
　　自身建设，事关人大及其常委会职权的正确行使和作用的充分发挥。本届全国人大常委会高度重视自身建设，把思想政治建设摆在首位，坚持正确政治方向，并以此引领人大各项工作。<br />
　　去年3月19日，十一届全国人大一次会议胜利闭幕的第二天，常委会即召开第一次会议，着重就把握人大工作正确政治方向等问题提出明确要求。之后，又专门安排时间，认真学习党的十七大精神和胡锦涛总书记有关重要讲话，进一步统一思想，提高认识。<br />
　　常委会组成人员、专门委员会组成人员一致认为，人大及其常委会是国家权力机关，是十分重要的政治机关。坚持正确政治方向是做好人大工作的根本。人大工作坚持正确政治方向，最根本的是坚持党的领导、人民当家作主、依法治国有机统一，核心是坚持党的领导。<br />
　　人大及其常委会依法集体行使职权、集体决定问题，坚持正确政治方向要靠大家共同来把握。在人大工作，坚持中国特色社会主义道路，坚持和完善人民代表大会制度，必须进一步认识我国人民代表大会制度与西方资本主义国家政体的本质区别。一要充分认识人民代表大会与西方议会的本质区别。我国的政党制度，是中国共产党领导的多党合作和政治协商制度，不是西方的多党制。中国共产党是领导核心，是执政党，各民主党派是参政党。县乡人大代表是按选区直接选举产生的。县级以上人大的代表由下一级人民代表大会选举产生，并按照选举单位组成代表团参加代表大会。<br />
　　我们的人民代表大会中没有议会党团，也不以界别开展活动。无论是代表大会，还是常委会或专门委员会，都不按党派分配席位。我们的人大代表、常委会组成人员、专门委员会组成人员，无论是共产党员，还是民主党派成员或者无党派人士，肩负的都是人民的重托，都在中国共产党领导下依法履行职责，为人民服务，根本利益是一致的。<br />
　　二要充分认识人大和“一府两院”的关系与西方国家国家机关间关系的本质区别。我国是由人民代表大会统一行使国家权力，“一府两院”由人大产生，对人大负责，受人大监督。各国家机关分工不同、职责不同，都在中国共产党领导下、在各自职权范围内贯彻落实党的路线方针政策和宪法法律，为建设和发展中国特色社会主义服务，不是西方的“三权分立”。<br />
　　人大根据党的主张和人民的意愿，通过制定法律、作出决议，决定国家大政方针，并监督和支持“一府两院”依法行政、公正司法，保障各国家机关协调有效地开展工作，把人民赋予的权力真正用来为人民谋利益。<br />
　　三要充分认识人大代表与西方议员的本质区别。我们的全国人大代表，来自各地区、各民族、各方面，人口再少的民族也至少有一名代表，具有广泛的代表性，不像西方议员是某党某派的代表。我们的人大代表，生活在人民中间，同人民群众保持密切联系。我们的人大代表，有各自的工作岗位，对党和国家的方针政策、宪法法律的贯彻实施情况体会最深刻，对现实生活中的实际问题了解最深入。我们的人大代表，是通过会议的方式依法集体行使职权，而不是每个代表个人直接去处理问题，各级人大常委会办事机构是代表的集体参谋助手和服务班子。<br />
　　大家强调，坚持人大工作正确政治方向，核心是坚持党的领导。中国共产党是中国特色社会主义事业的领导核心。党的领导只能加强，不能削弱。通过深入学习讨论，大家更深刻地领会了邓小平同志所指出的：“在中国这样一个大国，没有共产党的领导，必然四分五裂，一事无成。”<br />
　　“中国由共产党领导，中国的社会主义现代化建设事业由共产党领导，这个原则是不能动摇的；动摇了中国就要倒退到分裂和混乱，就不可能实现现代化。”等重要论断，进一步增强了坚持党的领导的自觉性和坚定性，增强了坚持走中国特色社会主义道路的自觉性和坚定性，增强了坚持和完善人民代表大会制度的自觉性和坚定性。<br />
　　要通过人大工作，确保党的主张经过法定程序成为国家意志，确保党组织推荐的人选经过法定程序成为国家政权机关的领导人员。人大各项工作，都要有利于加强和改善党的领导，有利于巩固党的执政地位，有利于保证党领导人民有效治理国家。大家指出，中国特色社会主义政治发展道路，是中国共产党领导中国人民选择的符合我国国情的唯一正确道路，是我国发展社会主义民主政治的唯一正确道路。我们深化政治体制改革，是不断推进包括人民代表大会制度在内的社会主义政治制度的自我完善和发展。要积极借鉴人类社会创造的文明成果包括政治文明的有益成果，但绝不照搬西方那一套，绝不搞多党轮流执政、“三权分立”、两院制。<br />
