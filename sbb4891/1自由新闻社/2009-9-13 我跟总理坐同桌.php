																											<div id="postmessage_238569" class="t_msgfont"><a href="http://www.chinanews.com.cn/edu/news/2009/09-13/1863502.shtml" target="_blank">http://www.chinanews.com.cn/edu/news/2009/09-13/1863502.shtml</a><br />
<br />
这是一组来自三十五中初二(5)班学生的作文，小作者们带着惊喜和幸福心情，从不同侧面讲述了与温爷爷在一起上课时感人的细节。让我们与这些学生一起感受共和国总理那亲切朴实、仁爱忧民的情怀，还有对教育和未来一代的重视和期望。<br />
<br />
　　我的同桌是总理<br />
<br />
　　三十五中初二(5)班　◎常子宜<br />
今天是9月4日，从早晨醒来，睁开双眼，还是平常的一天，真的是再平常不过。怀着一颗平常心，却得知了一个不平常的消息：今天，温总理要来我们学校，亲身感受当前中学课堂教学的实际情况。我不禁为之一振，心里想：“温总理是一个国家的总理，天天为国事操劳，还要视察学校，真是辛苦！”<br />
<br />
　　到了音乐教室，我的右边座位空着，我心里一惊，不会是总理要坐在这里吧？！我满脑子都在想：一会儿温总理来了，我应该怎么向温总理问好？我该说什么呢？……想着想着，便看见很多领导和记者从门外进来。也是在突然之间，一个在电视上看到过无数次的熟悉的脸庞映入眼帘，温总理来了！<br />
<br />
　　随后，温总理径直向我旁边的座位走来，当他经过我身旁的时候，我从内心深处发出一种声音：“温爷爷，您好！”总理微笑着，对我轻轻说了一句：“你好！”接着又亲切地对我说：“我今天就跟你同桌吧！”我幸福地笑着，因为今天我的同桌是总理。<br />
<br />
　　已经上课了，我的内心却还是激动不已，无法平静。这节音乐课是新学期的起始课，老师先就此学期要学习的音乐课提出了几个简单的问题。要是搁在平时上课，我会大胆地举起手来，但是，今天……不知怎么了，手是怎么也举不起来，面对这样的僵局，温总理小声对我说∶“你怎么不举手啊？”这大概是来自于长辈的嗔怪吧？！如此的亲切温暖。最终，我不知为什么还是没能举起手来，老师却叫了我的名字。我站起来的瞬间，耳边响起了温总理亲切而爽朗的笑声，这似乎是对我的鼓励吧……<br />
<br />
　　接下来，我们则聆听了“The King of pop”――Michael Jackson 的歌曲《We are the World》。当音乐进入高潮，温总理轻声地与我谈起了Michael Jackson。总理问了我几个关于Michael Jackson的问题，我一一回答了。随着音乐声结束，我突然想到一句话：“爱他们，从了解他们开始！”这句话是多么适合我们亲爱的温总理啊，一个国家的总理，为治理国家，他首先便选择了了解。<br />
<br />
　　终于进入了课堂的主旋律――《让世界充满爱》，我们跟随着旋律歌唱着“爱”，总理也在轻声哼唱，一首“爱的歌”把整堂课的主题升华。<br />
<br />
　　在整堂课快进入尾声时，老师邀请两位男同学、两位女同学合唱《让世界充满爱》。这一次我骄傲地举起了左手，从容地走到台前，用心把这首歌唱给总理听，那时，我没有一丝恐慌，因为我想台下有一个平和而且温柔的眼神在鼓励着我……<br />
<br />
　　40分钟一晃便过去了，总理又对我们讲了一番话。我们认真地倾听着，生怕落下一个词语……最后，总理对我们说：“我爱你们！”我们也齐声回应道：“温爷爷，我们爱您！”幸福，便在总理的脸上绽开。<br />
<br />
　　接着，我们又一起唱起了《幸福拍手歌》，在欢快的乐曲中，我想了很多很多，并在心里大声地说：“温爷爷，您整日奔波，为国事操劳，请您一定要保重身体！”<br />
<br />
　　我望着温爷爷，幸福洋溢在我的心头，也就是在瞬间，这种幸福传递给了每一个人……<br />
<br />
　　温爷爷和我们做游戏<br />
<br />
　　◎三十五中初二(5)班　冯梦溪<br />
<br />
　　早上来到学校，当老师告诉我们温总理要来学校和同学们一起听课时，每一个人都睁大了眼睛，欣喜若狂，激动不已。在短暂的早读课上，我们跟随老师朗读语文课文《芦花荡》，同学们都神情专注，大声地齐读，仿佛大家都沉浸在作者孙犁那优美的文字中……在朗朗的读书声中，敬爱的温爷爷面带微笑走进我们班教室：和我们常常在电视上看到的温爷爷一样，他是那样的和蔼可亲，是那样的平易近人，只是这一次感觉更加真实，因为他就近在咫尺。<br />
<br />
　　第一节课数学课，老师讲的是全等三角形的判定方法，我的眼睛始终紧紧盯着老师，并随着老师的提问认真思考，积极举手发言。这节课我上得比其他任何时候都专注，甚至连一秒钟都没有走过神。我希望班主任徐老师因我们的专注听讲而不会紧张、自如授课；我更希望我们初二(5)班每一个人超常发挥，能为我们三十五中增光添彩。<br />
<br />
　　连课间我们也和往常很不相同，班里的同学不再追跑打闹、无所事事，每一个人都放慢脚步，轻声细语；还有的同学围站在温爷爷身边，礼貌热情地回答温爷爷的询问。真没有想到温爷爷在百忙之中还关心着我们普通中学生的学习和生活。温爷爷走到讲台前，语重心长地对我们说：“孩子们，学习是幸福的。唯有好好学习，将来才能成为国家的栋梁之才。”言语间，让我深深地体会到了他老人家对我们、对未来中国的殷殷期望。<br />
<br />
　　最让我难忘的就属音乐课了！这节课上，我们和老师学唱《让世界充满爱》，在老师一遍遍哼唱和带领下，我们很快将歌词背下来，全班同学一起合唱： “轻轻地捧着你的脸，为你把眼泪擦干，这颗心永远属于你，告诉我不再孤单……”我们在歌声中传递着友情，传递着浓浓的爱。最后我们还和温爷爷一起做《幸福拍手歌》的游戏：“如果感到幸福你就拍拍手，如果感到幸福你就拍拍肩……”那一刻，每一位同学开心不已，幸福无比，敬爱的温爷爷的脸上也露出了慈祥的笑容。<br />
<br />
　　中午，温爷爷还愉快地和我们一起在学校食堂就餐，他真是一个可亲可敬、没有一点架子的总理爷爷！在离开学校之前，我们聆听爷爷的谆谆教诲，温爷爷大声地告诉我们：“孩子们，我爱你们！”那声音具有无穷的磁力，深深植入我们每一位老师和同学的心。<br />
<br />
　　慈祥可亲的温爷爷来到我们学校，并和我们一起上课、游戏、吃饭，这一天，我终生难忘！<br />
<br />
看着你们，我想起了山区的孩子们”<br />
<br />
　　◎三十五中初二(5)班　王静怡<br />
<br />
　　那天早上我背着书包迈着欢快的步子去学校，细雨绵绵，轻轻的雨滴显得格外温柔，又是一个凉爽的天气。<br />
<br />
　　好天气，好心情，来到学校，突然觉得今天的学校与往日的格外不同，走进教室，早到的同学和老师好像都在焦急地等待着什么。我依旧像往常一样走到自己的座位旁，准备着上课用的书本。听说今天有人来我们班听课，这样的听课经常有，已经习以为常。但今天同学和老师都好像很兴奋和紧张，还有同学在小声地议论着。旁边的同学兴奋地压着嗓子对我说：“知道今天谁来咱们班听课吗？”“不知道。”“是Wen Jiabao爷爷！”“什么？！”我几乎不相信我的耳朵了。顿时我的心紧张到了极点。温爷爷要来听课？！我不知道怎么形容我的心情。正在我忐忑不安的时候，上课的铃声响了，我们马上坐到了课桌前。<br />
第一节课是数学，老师站在讲台前，用激动的声音介绍道，Wen Jiabao爷爷来我们班听课，教室里顿时响起了热烈的掌声，温爷爷带着慈祥和蔼的微笑走进了教室，为了不影响我们上课，温爷爷坐在了我们教室的最后面一排。我兴奋得难以形容，多么幸运的事情，多么不同寻常的经历，我居然和总理温爷爷坐在一个教室里，我的心情久久不能平静。<br />
<br />
　　开始讲课了，平时喜欢的数学课，好像今天不知道老师在讲什么。<br />
<br />
　　这怎么行，我不停地在提醒自己，要好好地听，认真地听，万一回答问题时出了错，温爷爷会怎么看我。慢慢地，心情平静了下来，我认真地做着笔记，认真地思考着老师提出的问题，在平时觉得很长的一堂课，今天怎么这么快就下课了。<br />
<br />
　　接下来的每节课，都是在这么紧张和兴奋中度过的。有温爷爷在教室坐着，却让我更集中精力听课。每节课的知识都那么清晰明确。特别是在音乐课上，在《让世界充满爱》的音乐声中，温爷爷的一席话让我感想颇多。他说：“看着你们上课就想起了我的童年，我的童年是在一个战乱的年代；看着你们上课，我又想起了山区的孩子们，让贫困山区的孩子们能够上学是我们大家的愿望，现在这个愿望也已经基本实现了。你们每个人都要用自己的爱心来温暖这个世界，来创造这个世界。”<br />
<br />
　　多么让人感慨的一席话！我们要珍惜现在这么好的学习条件，更不能忘记那些在贫困山区和我们一样的孩子们。我们要用我们学习的知识来创造这个世界，付出我们的热情，让世界充满爱。<br />
<br />
　　多么不平凡的一天，多么与众不同的一天，多么难忘的一天。这么美好的一天让我久久不能忘怀。<br />
<br />
　　下课时给我们归纳要点<br />
<br />
　　◎三十五中初二(5)班　田嘉硕<br />
<br />
　　2009年9月4日，这一天，我永生难忘。<br />
<br />
　　今天并不是一个特殊的日子。太阳照常升起，早上的长安街依旧堵车，红绿灯分秒不差地闪烁着，我也和往常一样骑着车子向校园奔去。<br />
<br />
　　令我感到奇怪的事发生了，刚进班就发现多了一行桌椅，楼道里也站着几个陌生的人，老师们穿的衣服特别漂亮。<br />
<br />
　　当广播里说亲爱的温总理要来时，我还半信半疑――以前常常在新闻联播和网上看到温总理亲切的笑容，在学校真的可以看到吗?究竟会是什么样呢?而当班主任说温总理就坐在我的后面时，说实在的，我都被吓傻了，能够跟国家领导人一起上课呀!我的心里是又紧张，又激动。<br />
<br />
　　早读的铃声敲响了，我们拿起语文书，开始大声地朗读课文。就在这时，我听见后门不时传来快门声，然后身后的桌椅动了一下，随后传来如雨点般的快门声――温总理来了！<br />
<br />
　　我回头看了一下：“温爷爷好!”老人用一个笑回应了我，虽然温爷爷已经年近七旬，但是浑身上下非常干净利索。虽然黑中带白的稀发显得有些沧桑，但是总理那慈祥、和蔼的笑容却掩盖了这一切。我还是照常读书，总理在我后面不发一声地聆听着美妙的童声……<br />
<br />
　　数学课是早上的第一节课。我们的老师徐徐地从门外走进来，不紧不慢地说:“同学们好!”我们也恭恭敬敬地说：“老师好！”还不忘回头:“温爷爷好!”温爷爷也用一个笑容告诉了我们也很高兴来到这里。就这样，前四节课我们和总理一起上课，一起聊天，犹如神仙一般，好似梦的感觉。总理话并不多，但是每一句都能讲到理上。总理的笔记也是非常精炼!他在下课给我们归纳课上的每个要点，我们也是听的如痴如醉。第五节课，我们去音乐教室上，我还是坐在总理的旁边。当老师要求我们坐直时，总理也用力地挺了挺腰――真的是好可爱!<br />
<br />
　　最后，总理在我们面前，还是那动人的微笑，还是那利索的着装，对我们深情地说:“同学们，我爱你们!”<br />
<br />
　　总理，我们也想对您说:“我们爱您!”<br />
<br />
　　今天此生难忘……<br />
<br />
　　深情的讲述，感染我们每个人<br />
<br />
　　◎三十五中初二(5)班　申吉瑞<br />
<br />
　　2009年9月4日，是一个令我永生难忘的日子。<br />
<br />
　　上午8点多钟，Wen Jiabao总理在有关领导的陪同下来到我们班听课。总理的到来使同学和老师们都兴奋不已。第一节上数学课，温总理聚精会神地听着徐老师讲课，目不转睛地盯着投影，并不时地做一些笔记。下课后，同学们争先恐后地围过去，与温总理握手。总理还同王文杰同学一起研究了一道数学题。这让我感到了总理虽身居高位，但依然平易近人，让我们每一个人都感到亲切、自然。<br />
<br />
　　第二节语文课上，我们学习《芦花荡》一文，温总理认认真真地抄下了老师的板书。下课后我来到温总理身旁，这是我第一次近距离与总理交谈。我发现由于日理万机，过度操劳，他老人家两鬓已经有些发白，虽然面容也有些疲倦，但他的双眸依旧是那样明亮，目光深邃、亲切，嘴角微微上扬。总理翻开语文书，翻到《芦花荡》一课，询问我们是否第一次学习此课，需要学几个课时，是作为精读还是泛读学习等等。我一一详细地回答了总理的问题。<br />
<br />
　　第五节音乐课后，温总理语重心长地为我们讲了一段话。总理声音轻柔、语速缓慢地讲道：“在我小的时候，我爷爷创办的学堂被战火摧毁了，那时的我们是不幸的，而现在的你们是多么的幸福。前些年我到甘肃靖远县，一位双目失明的母亲拉着我的手哭个不停，我问她有什么困难，她只说了一句话――让我的孩子上学。孩子们，你们可以接受良好的教育，希望你们能够珍惜。我爱你们，也祝福你们！”温总理深情的讲述，感染了在场的每一个人，他的一字一句，如春风化雨，洒进了我们这些莘莘学子的心田。<br />
<br />
　　诚挚的叮嘱，殷切的期望，温总理对我们的关心与希望，让我们感到了一份厚望与责任。我一定会努力学习，成为国家的栋梁。<br />
<br />
朴素的白衬衫，朴实的话语<br />
<br />
　　◎三十五中初二(5)班　李<br />
<br />
　　清晨，我踏着欢快的脚步迈进校门。依旧是温和的门卫，来去匆匆的老师和学生。可是，不对！一定有什么不对。现在是7点5分，怎么大家都来得这么早？每个老师的脸上都是抑制不住的喜悦。这是为什么呢？<br />
<br />
　　当我被告知温爷爷要来的时候，我就那么呆呆地看着老师，重复问了两遍：“谁？”真是难以置信！<br />
当第一节课的预备铃响起，楼梯口传来一阵人群骚动的声音和照相机“咔咔”的声音。没人敢回头，没人敢再言语，就这样寂静，我们知道，是温爷爷来了。我大着胆子回头看了一眼，震惊！就他一个人坐在最后一排，脸上挂着无比温和的笑意，一件朴素的白衬衫，丝毫没有架子。这就是让我们敬仰的温总理啊！<br />
<br />
　　紧张啊，从小到大，这阵势还从没见到过。每一节课，我都无比专注也无比分神地听着。一下课，把书收拾好的几秒钟，温爷爷身边已经挤满了人。大家开始“疯狂”要签名。<br />
<br />
　　最后一节是音乐课。我们班走在前面，总理走在后面，周围站满了别的班的同学。他们带着惊羡得像是“杀人”的目光。是啊，总理一上午跟我们班在一起，已经让他们羡慕死了。<br />
<br />
　　这堂音乐课，我们学《让世界充满爱》，我想起了“5・12”，想到了雪灾……于是，我想到了一位老人。是谁带着全国的人民的祝福走进一片废墟，是谁不眠不休，用嘶哑的嗓子告诉大家：“中央没有忘记这些地方。”他就坐在我们身后。他是人民的公仆，是我们最亲和、最辛苦的总理！温爷爷，我们爱您！<br />
<br />
　　您说，我们让您想起了童年。这句普通而朴实的话语却让我想落泪。令人崇敬的总理原来也是一位慈祥和蔼的老人！<br />
<br />
　　日照当空，我目送着Wen Jiabao爷爷年迈而挺拔的身影愈行愈远。<br />
<br />
　　这一天，会永生难忘。<br />
<br />
　　把所有美好定格在那一刻<br />
<br />
　　◎三十五中初二(5)班　宋文础⊥跫秧<br />
<br />
　　一大早，进入教室后得到的消息让我震惊：我们的温总理温爷爷来我们班听课！<br />
<br />
　　平常人很难有幸见到总理一面，而我们却不知怎么竟拥有这样的殊荣。<br />
<br />
　　进来了！这就是我们的总理呀！一位慈祥的老人，让人有一种说不出的亲切感。<br />
<br />
　　温爷爷从第一节课听到第五节课，也许是由于总理在一旁，上课的时候总会有一种紧张感和压迫感，这使同学们的回答也有了一丝谨慎，生怕会答错了问题。有的答案会使总理微微一笑。比如在研学课的时候，老师问什么样的课程会让同学们有兴趣，田嘉硕回答：“学中带玩，玩中带学的课程。”全班同学都笑了，总理也笑了。<br />
<br />
　　下了课，后面的男同学很热情地去和温爷爷讨论问题，我也看到了温爷爷慈祥的笑容。好多同学都聚到Wen Jiabao爷爷坐的位置，所有的记者也都拿着大话筒放在高处。Oh, my god! 那场面，真是第一次见啊！记忆最深的是最后一节课――音乐课。温爷爷和我们一起上，最后下课的时候温爷爷和我们讲了话，温爷爷说：“看到你们，仿佛回到了我的童年。”温爷爷说话语速很慢，但说的话很有道理，从温爷爷的眼神里看得出他很爱我们和老师。一位慈祥的老人，一位可爱的老人。最后温爷爷还与我们合了影，所有的媒体都在那里拿着专业相机咔咔地照，仿佛要把今天上午所有的美好都定格在那一刻。<br />
<br />
　　Wen Jiabao爷爷来到我们学校，那时的我无比自豪、无比幸福，看到了这么一位慈祥的老爷爷。<br />
<br />
　　那一天的上午，已经定格在我们的记忆里。<br />
<br />
　　平常的一句话，听起来很震撼<br />
<br />
　　◎三十五中初二(5)班　张维嘉　邬炯<br />
<br />
　　是谁，在五四青年节之际，对着100多名清华学子晓之以理：“什么能够使你们的心灵永远明亮？那就是你们的理想和信念，把自己的一生献给人民！”<br />
<br />
　　是谁，在甲流肆虐之际，一天之内航行一万五千公里，横跨亚欧大陆，完成了一次旋风式的“诚意之旅”？<br />
<br />
　　又是谁，在汶川地震余震未消的时候，就奔赴前线亲自指挥？<br />
<br />
　　是他，Wen Jiabao爷爷。<br />
<br />
　　在我们这些学子心中，那些国家的领导人，从来都是高不可攀，只在电视上才能知道行踪。<br />
<br />
　　可惜，我们错了。9月4日，那个“高不可攀”的人，就站在了我们面前。这位年近七旬、和蔼可亲的温爷爷为了我们这些学生专程到我们中间，陪伴我们上完了五节课，与同学们同学同乐同食。我们非常感动！<br />
<br />
　　总理离我只有两个座位的距离，因此我能清楚地看见他那慈祥的脸庞。我对自己的动作非常拘束，生怕自己在温总理面前出错，那不是给三十五中丢脸吗？所以没有把握的问题我不敢举手发言，有时就连有把握的问题我也不敢举手，况且在那些摄影师面前我就更不敢发言了。所以下了课我只得安静地坐在座位上，做下节课的准备。<br />
<br />
　　随着第二节课的上课铃响，语文老师走进了教室。从外表看，老师很从容，但老师的心里却很激动，因为在语文老师面前的是我们的温总理。这前两节课全班没有一个随便说话的，可能是因为有总理在吧，也可能是因为摄影师在拍摄吧，也可能……总之，表现近乎完美。<br />
<br />
　　一下课，我们全都不约而同地向最后一排蜂拥而上，向温总理问好。温总理像一位慈祥、温和的老爷爷，会问我们叫什么，学得好不好，喜欢什么……<br />
<br />
　　“轻轻地捧着你的脸，为你把眼泪擦干，这颗心永远属于你，告诉我不再孤单……”《让世界充满爱》的歌曲，温柔又透着淡淡温暖，让不少同学的眼睛有些发涩。“和同学们一起上课的时候，让我想起了很多。”音乐课结尾时，温爷爷说，“想起了我的童年，想起了我的父母，想起了……”轻轻的声音，一如窗外的雨，温柔地落在我们心里。<br />
<br />
　　温爷爷的话依然回荡在耳边：“你们一定要好好学习，做国家的栋梁之材，祖国的明天是你们。”这种话，我听得多了，小时候父母说，长大了老师说。但哪一次也没有这样震撼过。是的，我们必须，也只有按照那四个字去做，听温爷爷的话――好好学习。（来源：北京青年报 ）</div>

							
							
							
							
