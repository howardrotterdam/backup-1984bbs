<br />
　　随后，该校招生就业处副处长路瑞峰打来电话，说已向文法学院党委书记核实，该院与毕业证挂钩的要件只有交齐学费和成绩合格两项，没有强制学生拿就业证交换毕业证。<br />
<br />
