南京数千学生抗议持续四日<br />
 <br />
南京工业技术学校至少两个校区的数千名学生，不满校方违背承诺拒发大专文凭，自上周四起一连四天进行抗议，包括上街游行、砸毁校舍，并与约400名公安和武警冲突，据称有数十名学生受伤。校方多位负责人星期二对本台表示，事件已经平息，目前正在追查幕后操纵者。 <br />
据自由亚洲电台报导，南京工业技术学校晨光和上元门两个校区的数千名学生，不满校方未履行给他们大专学历的承诺，在致电市教育局投诉，但无人理会的情况下，自上周四起，晨光校区的学生在校园抗议，但仍然无人与学生对话，于是上元门校区的学生也加入抗议，他们星期六从上元门游行到中央门，结果与到场公安对峙，继而发生冲突。据称有数十名学生受伤。记者星期二致电该校办公室查询，接听电话的一位负责人表示，事件已经结束，但拒绝透露内情：“这件事已经早结束了，南京市所有的领导、省委、省政府都知道这件事情，我是不会回答你任何问题，因为我不知道你是不是记者。过来，到学校来我慢慢跟你讲，记者证带过来，亮明你的身份，我再跟你解释”。 <br />
<br />
据上元门校区的一名职员表示，抗议的学生是晨光和上元门校区：“晨光校区应该是前一两天吧”。 <br />
<br />
记者：星期天的事吧？<br />
职员：应该事吧，现在应该没什么事啦，我们在这边听到消息说，应该是平息了。<br />
记者：学生星期四就开始抗议啦？<br />
职员：不是这边（上元门校区），这边就是有点动向，（晨光校区）星期四开始。<br />
记者：两个校区同时闹的？<br />
职员：啊，可能吧，还有一个就是我们这边，你打电话的这边，上元门。<br />
<br />
据大陆互联网论坛贴出的消息说，由于校方和教育局不肯谈判，抗议活动由初期的几十人，发展到数千人，学生怒砸行政楼、教学楼、超市。在网络流传的照片显示，多间校舍玻璃被砸，武警消防车及防暴警察在场戒备。其中上元门校区的抗议，从周日晚上七点多一直持续至凌晨五点多，直到约四百名公安和武警到场架起水枪驱散人群，抗议方休。 <br />
<br />
该校办公室一名负责人接受本台查询时，先是否认，后指是“不法份子在幕后操纵”：“由于不法份子利用学生的无知，煽动学生在学校发生混乱，带头的学生已经让家长稳定下来了，目前正在查幕后操纵者”。<br />
<br />
记者：学校有多少门窗被砸坏的？<br />
负责人：有个别的，有两台电脑，有点损失。<br />
<br />
记者：当时有多少学生参与？<br />
负责人：就是带头的几个学生，围观的特别多，然后围观的学生挺多的，一个校区就有一千多人。<br />
<br />
记者：原因究竟是什么？<br />
责任人：原因的话，这个我们不对外公布。<br />
<br />
据该校一名职员说，学生责怪校方不给他们大专文凭，但该校只有三年中级技工和五年高级技工课程。高级技工文凭等同大专文凭。而抗议的学生来自与他们合作的另外一所学校，因此发生误会：“他们那个是两回事，以前我们跟别的学校合办的，以前他们有直接拿大专文凭的那种课程，但是他们搞不明白，因为我们这边现在要参加成人高考（合格后才发大专文凭），他们以为‘高级’就是大专，高级职称只是相当于大专文凭”。<br />
<br />
而该校另一名招生办的负责人却将高级文凭课程说成是大专文凭：“中专（中级技工三年课程）学费一年是2200元，大专（高级技工课程五年）的前三年收费跟中专是一样的，最后两年是按照大专的收费，每一年收费是3600元”。<br />
记者：既然是大专，学生为什么说校方没有给他们大专文凭？<br />
职员：那我不太清楚。 <br />
<br />
据了解，学生抗议发生后，校方为防止事态进一步扩大，于星期一宣布放暑假。</div>

							
							
							
							
