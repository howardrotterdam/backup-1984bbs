&nbsp; &nbsp;&nbsp;&nbsp;当时是4点钟左右，我们放学后走到一个小胡同里，地震就开始了，当时也不知道什么叫地震，也没有那个概念，就晃起来一晃一晃，那幅度大的能把人晃倒，当时我还很小，那墙头很矮，我就扒着那个墙头，来回在那里晃，以后等那地震结束以后，就看那个各村起的那个烟，就跟战争起的一样，都是黄烟，树木也看不到了，所有的什么一片都掩起来了。 <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;地震局地球物理所专家朱传镇当时在现场也目睹了同样的景象。&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;地震局地球物理所高级工程师朱传镇: <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;3月22号，一晃动，我就跑出帐篷一看，一包烟，整个村子一团浓烟起来了，就等于像一个大炸弹炸响了。我们就奔向那个村里去看看情况，去救救人。 <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;宁晋酒厂成松森： <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;跑到家里以后吧，看到各家房子倒的都不少，尤其是土坯房，基本上都倒平了，把各家各户的锅碗瓢匙所有被褥全都埋在底下了。 <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;地震局地球物理所专家朱传镇: <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;我一看,我的第一个印象,就像经历了一场战争一样，房子全垮掉了，一看全是一个战场，人死的死，有的呆的呆，一些老头没死的也坐在那里一声不吭，或者见了我们哎呀,哭啊，家里全完了。怎么活下去，这种场景跟一场大的战争之后的情景一样，但这不是战争，是天灾。 <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;经测定这次为7，2级强烈地震,地震的震中位于宁晋县的东汪镇，以此为中心的极震中137平方公里内房屋倒塌百分之九十以上。其中东汪镇房屋全部损毁。 <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;这次强震的破坏范围涉及石家庄，衡水，邯郸等6个地区136个市县，近两万个村庄，倒塌房屋500万间，并造成了许多工厂，学校，道路的损毁，滏阳河，小漳河等多处桥梁，水利设施遭到严重破坏。 <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;一些有千百年历史的古代建筑也损毁严重，&nbsp; &nbsp;<br />
<br />
宁晋县灾情 <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;宁晋县多处石牌坊塌落， <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;正定县宋朝木塔震落两层， <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;赵县柏林寺塔当年曾震掉了铁顶，许多千年古迹毁于一旦， <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;惟有建于隋朝的赵州桥，历经千年风雨依然如故。 <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;这次强烈地震的有感范围北达内蒙古多伦，南至江苏南京，东至烟台，西到铜川，面积达200万平方公里，有1亿两千万人程度不同的感知了这次大地震。 <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;所幸这次地震发生在白天，多数人在户外而且有了前次地震的教训，采取了一定防震措施，因此伤亡要少了许多。 <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;据最终统计，邢台大地震共计造成8182人死亡，受伤人数达38675人。地震还造成了价值10亿元人民币的直接经济损失。 <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;然而强烈余震依然不断发生，3月26日宁晋又发生6，2级地震，3月29日邻近的巨鹿县又发生6级余震，邢台3月8日大地震后反6级以上强余震，既达到5次之多。 <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;人民日报报道了强烈余震的消息,并在编者按中把地震称为怪物. <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;河北省地震局高级工程师：王泽皋 <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;邢台地震是个什么样的地震呢，它是一个前震多，主震强，余震频繁，衰减有起伏，延续时间长的大型地震群。 <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;余震频繁,延续时间长是邢台地震的显著特征.就是这些特征,也使救灾工作困难重重.<br />
