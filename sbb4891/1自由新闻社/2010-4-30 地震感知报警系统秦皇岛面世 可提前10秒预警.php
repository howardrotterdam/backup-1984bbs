<br />
举报一位天天在网上发布地震预报的江湖高人(2010-04-30 05:03:14)<br />
<br />
&nbsp; &nbsp; 昨天晚上深圳卫视“22度观察”地震预测辩论节目录制现场，有一位台下嘉宾（因为节目录制前见到此人在贵宾室，所以知道不是普通观众）站起来发言，问我：“方舟子，你知道我是谁吗？”我心想，是不是又遇到范良藻式的人物了？（几年前一虎一席谈辩论伪科学时，范良藻也是这么问我的。）虽然在贵宾室曾见到他和地震局前首席预报员孙士f谈得甚欢，但没有听到他的名字，只好老老实实地承认自己的孤陋寡闻：“不知道。”他又问：“你知道我是用什么方法预测地震的吗？”我反问道：“有必要知道吗？”他又说：“你刚才说地震预测都是伪科学……”我纠正说：“我说的是现在号称能准确预测地震的都是伪科学，至少是不科学。”他说：“那是你改口了。”然后慷慨激昂地说：“你不知道我是谁，不知道我用什么方法预测地震，你凭什么说我是伪科学，你自己就没有科学态度。”我张口要反驳（我想说的是：“如果有人跟我说他在研究永动机，我不必知道他是谁、怎么研究，也敢说他是在搞伪科学。”），被主持人打住，要我们听他说。于是此人就大谈自己预测地震的经历，发明了一个通过捕捉中微子来预测地震的仪器，曾经和国家地震局合作准确预测了攀枝花的地震，现在每天在博客上发布地震预报，请大家关注其博客云云。等他谈完了，主持人却不让我回应，把话题转开了，否则我想说：“现在听你讲完了，我更敢说你这是典型的伪科学。你要是用那么简陋的仪器能够捕捉到中微子，别搞地震预测了，拿诺贝尔物理奖去！”<br />
<br />
<br />
&nbsp; &nbsp; 后来，方玄昌谈到，“天地生人”搞的那个“灾害预测委员会”不久前曾在网上发布预测说4月28日北京、天津要发生大地震，已被证伪了，孙士f又有点气急败坏地指责我们无知，是法盲，不知道国家禁止个人发布地震预报，在这里发预报是要负法律责任的。我们反驳说，我们是在揭露谣言，如果要追究法律责任，要抓也应该抓发布预报的人，比如刚才说自己天天在网上发地震预报的（我指了指那个捕捉中微子的高人）。<br />
<br />
<br />
&nbsp; &nbsp; 今天读到此人一篇博客文章，吹嘘他是如何挑战我的：“此时全场响起了热烈的掌声……方舟子，一个被称之为反伪科学的斗士变得哑口无言，大汗淋淋，无语……尔后还叫喊、要抓乱测地震的就第一个把我抓起来。”才知道原来这位高人的大名叫做杨智敏（原是贵阳市京剧团下岗职工），而他也的确几乎天天在其搜狐博客上（<a href="http://aaaaa0166.blog.sohu.com" target="_blank">http://aaaaa0166.blog.sohu.com</a>）发布中国某地即将发生地震的预报，读者还很不少，每个预报帖子的点击都有好几万。他起初是毫不掩饰，直接发地震预报，后来大概经人指点知道这么做是犯法的，所以用了两个办法来遮人耳目：一个办法是给地震预报帖取一个政治正确的标题，例如“在党的领导下, 在胡主席的科学发展观的正确指引下”、“爱党, 爱祖国,”，如果只看标题，决不会想到其内容是在发布地震预报；另一个办法是用天气预报的形式发布，用“大雨、中雨”来表示“大地震、中地震”，用“风力”来表示震级，但其内容显然是地震预报。<br />
<br />
<br />
&nbsp; &nbsp; 据《中华人民共和国防震减灾法》第二十九条：“除发表本人或者本单位对长期、中期地震活动趋势的研究成果及进行相关学术交流外，任何单位和个人不得向社会散布地震预测意见。任何单位和个人不得向社会散布地震预报意见及其评审结果。”第八十八条：“违反本法规定，向社会散布地震预测意见、地震预报意见及其评审结果，或者在地震灾后过渡性安置、地震灾后恢复重建中扰乱社会秩序，构成违反治安管理行为的，由公安机关依法给予处罚。”杨智敏发布的是短期、临震预报，显然违法，而且让人给他捐款，更有骗钱之嫌。奇怪的是，此人自2007年9月就开始在其搜狐博客上发布地震预报，发了无数预报帖子，却不仅没有被公安机关请去喝茶，反而越来越嚣张，越来越风光，不仅和很懂法律的地震局前首席预报员打得火热，现在还能上电视推销自己了。相比之下，那些只是在网上发了一个地震预报帖子就被抓起来的，就太倒霉了。杨智敏能够如此超脱于法律之外，莫非是因为有地震局的“专家”当其后台？</div>

							
							
							
							
