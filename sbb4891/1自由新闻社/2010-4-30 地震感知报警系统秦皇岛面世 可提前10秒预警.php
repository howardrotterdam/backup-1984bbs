													<h2>佛山地震局称今年地震多发 建议倒立酒瓶预报</h2>
						
						
																											<div id="postmessage_591510" class="t_msgfont">海地地震、智利地震、青海地震，地震似乎离我们越来越近，也成为市民近期最关心的热点话题。为何最近地震频发？发生地震后我们该怎么办？昨天，佛山市地震局局长廖华康作客本报两会直播室，就近期市民关心的地震话题进行畅谈。廖华康表示，之前网上流传的磁铁预报地震的“山寨”预报法其实并不靠谱，倒是啤酒瓶倒立在桌上可以预报地震。很多市民不知道的是，楼层越高越安全。 <br />
<br />
<br />
震情：<br />
<br />
今年已进入地震高发期<br />
<br />
佛山市地震局局长廖华康表示，进入2010年后全球地震较多，可以用“频发”来形容。他表示，对地震的认识，我们只能根据资料进行统计，比如说全世界的地震大于7级的每年是8次。假设今年的地震次数多于8次，那就是地震比较多发了，假设少于8次，那就是地震发生比较少了。今年以来，从1月份到4月份全球7级地震已经发生了4次，可以说今年地球进入了地震频发期。<br />
<br />
分析：<br />
<br />
佛山是地震重点监视区<br />
<br />
廖华康表示，佛山历史上发生过5级地震，那是300多年前。佛山不临海，也不在环太平洋地震带的附近，比较靠近内陆。佛山也有断层经过，但是断层规模比较小，所以佛山虽然有发生地震的背景，但是也不会发生很大的地震。像7级以上地震的情况很少。<br />
<br />
因为佛山历史上没有发生过大于5级的地震，根据地质活动规律推测，即使佛山以后发生地震，大约也是五级、六级。因为发生大地震的地方，地质构造都比较复杂，断裂带比较多。佛山这一块地方包括珠三角都是比较平静的。所以从这个方面推算，佛山以后也不会发生很大的地震。<br />
<br />
但如果市民认为地震跟佛山无关，那也大错特错了。实际上，这些年珠三角的地震基本上都发生在佛山。佛山地质结构复杂，广州-三水断裂、从化-阳江断裂、沙湾断裂等区域性大断裂贯穿全区，历史上曾发生过30多次3~5级地震。最大一次地震是1683年在南海罗村发生的5.0级地震。最近一次发生的地震是1997年发生在三水南边的4.4级地震。佛山被国家列为国家级地震重点监视防御区。<br />
<br />
纠偏：<br />
<br />
“山寨”地震预报法不靠谱<br />
<br />
廖华康表示，现在地震预报是一个难题，佛山范围内有15个观测站，这些都是进行科学研究的，主要观测两类，一个是地下水，据说地震之前地下水会改变，水位会升高或者降低，或者水的成分发生变化，地震局24小时都在监测。<br />
<br />
再就是电磁波。电磁波的变化在地震前夕的变化十分明显。<br />
<br />
最近网上流传着一种“山寨”地震预报法，说是根据国际组织预报说今年地球进入地球地震年，所以他们给了一个地震预报的方法，把一块磁铁挂在高处，下面正对着地板或者铁盆，如果地震要发生了，磁铁的磁性就会消失。对此，廖华康表示，这种“山寨”地震预报法没有科学依据。<br />
<br />
他表示，自己做地震监测工作整整十年了，实践发现，大地震之前电磁波是有改变的，但它的数量很少，只有仪器可以记录得到，人没有感觉。<br />
<br />
土方：啤酒瓶倒立或可预测地震<br />
<br />
那么，有没有一些简单实用的方法可以预报地震呢？廖华康表示，指南针是最直接的预报工具。<br />
<br />
有些人用指南针，地震之前就会偏一点，不是指向正北或者正南。所以用一些装置，假如指南针发生转变，偏了的时候触发一个装置，就可以拉响警报。<br />
<br />
还有些方法更简单。汶川地震之后，很多人在自己家里装一个感应地震的仪器，这个仪器很简单，甚至说不上算是仪器，就是用一个啤酒瓶倒过来放。地震一来一摇动,瓶子就倒了。<br />
<br />
这种方法具有可操作性的原因是灵敏度高，现在地震仪也是这个原理，也就是惯性原理，就是啤酒瓶的瓶底比较重，倒立后，只要地面稍有轻微震动，就会触发“机关”。<br />
<br />
佛山市地震局局长廖华康昨回应读者称：“防御工作做好了，地震什么时候来都不怕<br />
<br />
话你知：<br />
<br />
广佛的楼房可抗5.5到6级地震<br />
<br />
发生地震时先躲再逃<br />
<br />
一旦发生地震，选择躲在哪里才更安全？廖华康建议，先躲再逃，分两步。因为地震整个发生时间也就十秒。假设在一楼，十秒钟可以逃出去了，假设在五楼、十楼，十秒钟不可能逃出去。所以，当市民感觉要地震的时候首先要躲，等地震完了之后再逃。<br />
<br />
楼层越高越安全<br />
<br />
佛山新建的房屋可以抗七度地震，相当于5.5级地震。也就是说，假设广州、佛山发生5.5到6级地震，最近十年佛山新建的房子一点破坏都没有。楼层越高，其实越安全，顶多晃动几下。<br />
<br />
在逃生技巧方面，在房屋里面时，躲在跨度比较小的地方，比如走廊、厨房、洗手间，这些地方跨度比较小的，跨度小主要是有很多墙壁或者柱子撑住了，不会完全倒塌。相反，在阳台则较危险，在跨度比较大的客厅也不好。<br />
<br />
如果一时半会找不到合适的地方，在墙角也好一点，就是有一些支撑的三角地带，地震发生时墙体不会完全塌下来，存活的几率也会大一些。<br />
<br />
他山之石：<br />
<br />
预报地震难把功夫放在防御地震上更实在<br />
<br />
廖华康表示，当前一些先进发达地区，比如美国、日本，包括我国的台湾，地震比较多，经常听说他们那边有地震，所以他们现在也把注意力放在怎么来预防地震发生。刚才说了他们不会很重视预报，他们觉得地震预报是比较艰难的一项工作，所以把精力放在防御地震上更实在。<br />
<br />
比如说，日本有两类房子，一类是高楼，高楼就用钢结构，钢结构是有柔性的，地震来的时候不会倒，摇来摇去，韧性比较好。还有一个是木房子，居民住木房子，木房子也是柔性，摇来摇去，不会像水泥房子这样子，一摇就会倒塌；假设木房子，地震再大一点，塌下来，人也不会被砸得很厉害，而且木房子比较低，都是一层两层，高的房子一般都是钢结构的。<br />
<br />
地震到底能不能够预报？<br />
<br />
廖华康：地震不可预报<br />
<br />
昨天的直播尚未开始，网友留言便有数页。其中网友留言最多的是关心地震到底能不能预测？精确到什么程度？昨天，佛山市地震局局长廖华康就此问题回应读者称：“目前，没有一个国家能够做到对地震的准确预报！”<br />
<br />
“有人就是不理解，为什么地震局不能预报地震，当时我们说是不能预报的，但是群众不理解，说你知道了你不说。”廖华康说，为什么人们有这个思想呢？主要是很多人把地震预报跟气象预报相比，认为气象可以预报，为什么地震就不能预报呢？其实主要是人们对地震知识了解不够。<br />
<br />
廖华康明确地说，地震是不可以预报的，包括在未来一段时间里也不可预报。“我记得前十年左右，全国的地理学家达成一个共识，地震本质上是不能预报的，现在德国对地震从以前开展预报工作转向了防御工作，把防御工作做好，不管地震什么时候来，房子不倒，人不死，我这个减灾工作就有成效了，所以大家应把精力放在防御上。”<br />
<br />
地震前兆到底靠不靠谱？<br />
<br />
廖华康：有，但难确定<br />
<br />
网友whunews2012留言称，“我们听说，地震有前兆，比如猪、羊不进圈、狗乱叫、癞蛤蟆成群等。请问廖局长是不是地震一定有前兆？另外，可不可以这样理解，动物异常不一定是有地震，有地震一定有前兆？如果不是，那么有前兆的地震占地震次数的多大比例？”<br />
<br />
廖华康解释称，从目前掌握的情况来看应该说有。比如说唐山地震前，动物出现了很多的异常反应，但是从汶川地震、玉树地震来看，地震前兆并不是很明显，因此如何看待前兆问题，目前学界还没有统一的一个标准。“举个例子，小狗生病也很反常，也可能是地震前兆，但小狗生病在平常的时候比地震前生病的情况要多得多。假如你告诉我我家的小狗出现反常了，是不是地震，这样一个单一事例，我很难确定。”<br />
<br />
“我们只能尽量收集这些异常的反应，收集得多了，我们的判断准确率就会高一点”，因此，市民若发现身边出现异常情况，可以通过电话以及论坛、QQ群、电子邮箱、网站等告诉地震局的值班人员。廖华康现场向网友公布了佛山地震局的值班电话：83003221，“我们是24小时有人值班，市民有问题可以随时致电！”<br />
<br />
佛山准确预报了1996年的三水地震？<br />
<br />
廖华康：那是事后总结<br />
<br />
“在1995年，佛山三水高丰农场和顺德大良中学两口深水井有异常，最后你们认为1996年或1997年佛山可能会发生地震，果然不出所料，在1997年的9月23日和26日，佛山市三水南边镇先后发生3.7级和4.4级地震”，是不是佛山准确预报了那次地震呢？<br />
<br />
“上次其实也是事后总结的，不是提前说出来的，应该不算预报。世界上有城市曾经预测过地震，但是那是特殊情况，在别的地方就不适用了。”廖华康说，现在人类对地震的产生和发生过程，还是不了解。原来大家以为只要我们自己去认真研究，去总结，就能准确预报地震，但是都算不上成功，大家还在对地震的发生进行研究和探索。<br />
<br />
“像气象预报一样，每天记录温度、压力，就可以预报明天的天气。对地震则不是这样的，人类现在还没有找到一种跟地震有关的东西进行观测，到底观测什么可以预告明天的地震，找不到，也说不准。所以现在大家都在摸索，国家也鼓励广大群众参与地震预报的一些科学研究，所以不是说我们知道了不想告诉大家，其实我们也不知道。”廖华康说。<br />
<br />
三水乐平地震与挖空盐矿有关？<br />
<br />
廖华康：没有证据证明两者有直接关系<br />
<br />
网友“dayoo885609”问，“三水有人说，乐平地震是因为挖空了三水盐矿，导致了人为的重力下沉，可信吗？据说早在上世纪80年代，国家一份地震方面的专业刊物就来过三水调研，指出地震与盐矿有一定关系，而且还写成了文章发布，观点也上报到政府了。”<br />
<br />
廖华康说，现在还不能深入地下去了解事情发生的原因，三水在1997年发生了两次地震，这两次地震比较特殊，深度只有1公里，属浅源地震，一般地震深度是10公里以下。发生的时候，因为距地面很浅，所以破坏力很大。虽然是三、四级多的小地震，结果却造成很多农村居民的房屋破坏了。<br />
<br />
廖华康说，说来也巧，在震中附近确实有一个盐矿，大家提问，这个地震是不是跟盐矿有关系？到目前为止我们觉得依据还不是很充分，可能有关系，也可能没关系。因为盐矿就在它的附近，而且深度也差不多，另外为什么最近那个地方又发生地震，所以大家比较怀疑不是没有道理的，当然大家也很难拿出直接的证据来。<br />
<br />
地球是不是分为地壳、地幔、地核？<br />
<br />
廖华康：都是推想，认识地球比认识癌症还难<br />
<br />
有网友问“中学的地理教科书上讲，地球的结构分为地壳、地幔、地核，到底是不是这样的？”廖华康称，地球的分层都是人为推想的。对地球的认识是整个人类的一个难题，人类可以上天，去别的星球，但是对自己生活的地球还不了解。整个地球半径1600公里，现在人类打钻的最深处也只有12公里，两者相差很远。“12公里的距离连地球的皮毛还达不到，别说把地壳打穿了。教科书上所说地壳、地幔、地核，这些其实都是推想，人类给它一个物理的认识概念，因为谁也没有见过地下是什么样的。”<br />
<br />
廖华康说，人类不了解自己居住的地球，也不知道地球里面是什么东西，对地震的认识基本上可以说没有，目前我们的科学还达不到这个水平。“很多癌症是可以治好的，但是对地震的认识就要难得多，我们可以去了解的地震的情况实在太少了。”<br />
<br />
来源：大洋网-广州日报</div>

							
							
							
							
