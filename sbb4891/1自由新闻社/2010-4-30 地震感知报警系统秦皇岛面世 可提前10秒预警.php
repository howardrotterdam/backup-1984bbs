<br />
工作人员在展示“地震感知报警仪”。<br />
<br />
<img src="http://i2.ce.cn/district/zg/201004/29/W020100429592634090217.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
在河北省科技厅组织的科技成果鉴定会现场。(中国经济网记者雷汉发、通讯员李永利 摄) <br />
<br />
 <br />
<br />
&nbsp; &nbsp; 中国经济网秦皇岛4月29日讯(记者雷汉发、通讯员李永利) 4月28日上午，在河北省科技厅组织的科技成果鉴定会上，由秦皇岛市经济技术开发区前景光电技术有限公司自主研发的“地震感知报警系统”通过了专家鉴定。来自全国地震、建筑、电力电子与工程控制领域的专家一致认为，该系统可以提前10秒感知地震信息，为震前逃生和自救赢得宝贵时间。 <br />
<br />
&nbsp; &nbsp; 据该公司技术人员介绍，地震感知预警系统是由地震感知工作站、地震应急救生器和城市地震信息收集统计分析中心三部分构成。地震感知工作站其实就是一个区别不同应用范围的地震感知报警仪，它应用三轴动态地心垂直技术，以0.03秒以下的反应速度，实时检测地面运动加速度的异常变化，当震源发出的纵波和横波辐射到地球表面时，地震感知报警仪根据其强度，智能判断即将形成的面波将在本区域造成的地震震级，根据不同地区的地壳厚度及震源深浅不同，可提前10―30秒感知地震信息，并自动报警提醒逃生。<br />
<br />
<img src="http://i3.ce.cn/district/zg/201004/29/W020100429592634201523.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
地震远程监控数据分析中心。(中国经济网记者雷汉发、通讯员李永利 摄) <br />
<br />
<br />
&nbsp; &nbsp; 按照覆盖面的需要，地震感知工作站可区别家庭、小区、城市等各种范围进行装备。该公司同时研发的电梯地震监测系统，基于第三代移动通讯技术研发，由电梯地震监测仪和电梯地震监测网两部分构成，采用角度自动智能判断技术及同城网络式地震信息综合分析技术，具有地震信息快速网络自动播报功能。经河北省科技厅科技信息情报所科技认定，该系统在国内外也属首创。 <br />
<br />
&nbsp; &nbsp; 据悉，此次地震感知报警系统鉴定委员会由中国地震局全国地震灾害评估委员会委员、国家自然科学基金委“重大工程动力灾变”重大科学计划指导专家组副组长杜修力等7位专家组成。专家组一致认定：该项目对于减灾防灾具有积极的作用，具有良好的社会经济效益和应用前景，技术达到国际先进水平。<br />
<br />
