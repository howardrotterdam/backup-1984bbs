<br />
张某通过咨询中国移动客服处才得知，这一切都源于他向外发送了“黄段子”，因被相关系统检测到而被关停了短信功能。<br />
<br />
<br />
张某称，前不久，他接到一个朋友发来的黄色笑话，“觉得好玩就发给了另一个朋友”。没想到就受到了惩罚。如果要重新开通短信功能，就得带着身份证到公安部门写一份以后不发不良信息的保证书。<br />
<br />
对此，笔者致电移动公司客服电话咨询。话务员表示，日前，该公司正配合公安部门开展手机违法短信息治理，治理范围包括：假冒银行或以银行名义发手机违法短信进行诈骗或者敲诈勒索公司财务的，散播淫秽、色情、赌博、暴力、凶杀、恐怖内容或者教唆犯罪、传授犯罪方法的，只要涉及其中一项，哪怕只发送了一次， 被通讯公司检测系统发现或被客户投诉确认，就会被关停短信功能。</div>

							
							
							
							
