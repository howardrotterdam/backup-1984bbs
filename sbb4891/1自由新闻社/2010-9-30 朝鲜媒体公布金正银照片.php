《亚洲华尔街日报》--朝鲜最大报纸周四在头版刊登了金正银(Kim Jong Eun)的照片，据信这是朝鲜首次公开发布金正银成年后的照片。金正银是朝鲜领导人金正日的儿子和接班人。<br />
<br />
照片显示年轻的金正银坐在一批军事官员的头排。他的座位靠近自己的父亲，他们中间隔著一位胸膛挂满勋章的年长将军。<br />
<br />
照片上的金正银面颊丰满、不苟言笑，看上去显得紧张。他身穿一套黑色衣服，这种服装类似于他父亲那著名的卡其布运动服。<br />
<br />
外界对金正银所知甚少，也不知道他的确切年龄。他据信生于1983或1984年。以往外界只能看到他屈指可数的几张照片，而且都是十几年前拍摄的，照片上的金正银要么是生活在平壤的儿童，要么是在瑞士一家私立学校读书的学生。<br />
<br />
金正银的这一新形像是首次出现在朝鲜执政党劳动党的官方报纸《劳动新闻》(Rodung Shinmun)上，朝鲜官方通讯社周四下午发布了金正银的新照片，国际社会目前已能看到这张照片。<br />
<br />
由于金正银是与几十人一起合影，而且这张照片的清晰度不高，因此照片上金正银的模样不太清楚。<br />
<br />
即使如此，韩国报纸和网站还是立即开始将金正银的形像与他父亲金正日和他爷爷金日成的老照片相比较。金日成是朝鲜民主主义人民共和国的创始人，领导这个国家达四十年。<br />
