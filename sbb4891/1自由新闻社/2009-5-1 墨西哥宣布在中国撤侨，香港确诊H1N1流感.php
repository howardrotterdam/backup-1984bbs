　　笔者关注媒体近日有关“猪流感”的新闻报道，好多信息看了让人还是能“放下一颗心”的。比如：胡锦涛总书记作出重要指示，做好防范人感染猪流感疫情工作，确保人民群众身体健康和生命安全。国家卫生部紧急出台《人感染猪流感诊疗方案(2009版)》，指导人感染猪流感的诊断和治疗工作。卫生部部长陈竺29日表示，相信用科学的卫生知识“武装”民众，人感染猪流感可防、可控、可治。我国此前为应对人禽流感疫情已进行有效抗病毒药物的物资储备，目前已采取措施扩大有效抗病毒药物的储存数量，国内有足够的生产能力做到这一点。教育部、卫生部29日晚间发出通知，要求各级各类学校尽快开展一次人感染猪流感基本常识和预防知识的宣传教育活动，既要避免学校师生产生不必要的恐慌情绪，又要普及科学防范知识。上海市拟定首个猪流感防治预案，上海市公共卫生临床中心和复旦大学附属儿科医院被指定为定点医院，凡诊断为人感染猪流感疑似病例、确诊病例的，将由救护车送至这两家医院接受隔离治疗。 <br />
<br />
　　目前，能够让老百姓安心定心的好消息不少，各级部门防控工作有条不紊，我们还没有一例“猪流感”疫情，因此，五一出游、购物、休闲照样人流井喷。即便如此，我们还是不能有丝毫麻痹大意，防控工作来不得半点疏忽，只有抱着对人民群众身体健康和生命安全高度负责的态度，才能把防控工作措施落实到位，“防患于未然”。 <br />
<br />
