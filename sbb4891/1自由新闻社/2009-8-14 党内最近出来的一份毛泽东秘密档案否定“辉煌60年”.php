													<h2>党内最近出来的一份毛泽东秘密档案否定“辉煌60年”</h2>
						
						
																											<div id="postmessage_203427" class="t_msgfont">2009年8月13日 星期四 <br />
&nbsp;&nbsp;<br />
瞿咫 <br />
&nbsp;&nbsp;<br />
<br />
每个中共的高级干部都希望在生前把自己的经历写成回忆录，留下来。这包括2004年九十多名原省部级高干、上将等老干部及其子女，其中包括林彪的女儿林豆豆、黄永胜家属、江青的女儿李讷等。<br />
<br />
这本来是件很平常的事。但中共非常紧张，最近决定特拨两亿元国库银子，以党的名义收购下来。万里说过，至今党库和国库之间还没有建立一堵墙。如果真建立的话，那党就是赤贫。 <br />
<br />
毛泽东生前机要秘书张玉凤兼「陪床」，历时三年写就的回忆录书稿《回忆在主席身边的岁月》(暂名)，经中宣部、毛泽东思想研究室等单位审核四个月，最后决定：该书极不宜发表，其中某些二人的对话留作研究参阅。据悉，书稿内容涉及毛泽东与林彪、周恩来、江青等人之间不寻常关系的问题。<br />
<br />
全书八十多万字，虽曾通过一审，但却遭到毛泽东后人反对而暂搁。原因是毛泽东的女儿、侄孙坚决反对，指内容有损领袖形象。他们愿出一百万人民币买断版权，阻止出版。<br />
<br />
为什么呢？必然是有丑事被抖露，而且符合实际情况。<br />
<br />
据党内一份秘密档案揭示：文革初期，1966年8月15日，江青写信给周恩来，信中提到：毛泽东长期的放荡生活问题，已经给江青从精神、身心上造成创伤，要求组织出面解决。周恩来看后，不做任何表示，写条：「请送林彪同志」。林彪看后，批上：「请退交总理处理」。<br />
<br />
皮球踢来踢去也没踢出个结果来。那个时刻，毛是「最红最红的红太阳」，这种烫手山竽无人敢碰。<br />
<br />
一年后，1967年8月10日，此次江青同时写信给「永远健康」林彪和周恩来。再次提到：已经无法忍受毛泽东放荡生活，要求结束与毛的夫妻关系。<br />
<br />
并提出以自杀来表达对毛腐化堕落私生活的抗议。并批评党长期纵容毛泽东放荡生活。林彪在信上批示：「请总理劝导江青同志能顾全大局和主席的声誉。」<br />
<br />
两天以后，1967年8月12日，江青以有工作向总理请示的名义，带着两盒点心去中南海西花厅。此时毛正在中南、华东地区「巡视文革运动」。<br />
<br />
江青一到，就伤心大哭不止，说：「总理、总理，其实我的生活是不愉快的，是很苦闷的、很孤独的。您知道，在这个世界上，我能对谁说、对说控诉？想一想，在现代社会，哪有领袖不许自己妻子一起居住，不许自己妻子来看望？这简直是伟大领袖的伟大创造，这是什么党的工作需要，党的决定！是他(指毛)数十年放荡生活的需要，总理，您要为我作主，讲讲话。我不能再这样忍受着生活，我想，哪一天就死在中南海。」<br />
<br />
1968年10月7日晚，江青又去西花厅，向周恩来和邓颖超哭诉：毛不但继续淫乱放荡，而且下命令让她搬出中南海，未经准许，不准进中南海。很快江真的被迫搬出了。<br />
<br />
1971年9月13日，林彪、叶群和儿子林立果乘飞机失事，死在外蒙。9天之后，9月22日，江青说要与总理交心，希望邓颖超也参加，周只一人去了当时江青居住的钓鱼台10号楼。<br />
<br />
毛如何解释他的高瞻远瞩？！ <br />
江青谈到几个问题，让周倒吸一口凉气，她说：「我有三个要求，1、要主席亲自在政治局解释，毛说考察林彪30年，不会有偏，为什么既定接班人会外逃？2、主席不会万岁，也不会百岁，还要不要选定新的接班人，我坚持要。3、主席要作点自我批评，不要太霸道、太独裁，怀疑一切、肯定一切，反复无常。」<br />
<br />
江青谈到的这个不是个小问题，而是否定共产党政权的大问题，到今天为止，毛的尸体还占着天安门广场内一个很大的位置，躺在毛纪念堂里供人膜拜。这不是共产党真的搞什么个人崇拜，而是为了党的生存。这也是为什么毛的私人医生李志绥出书揭露真实的毛泽东时，中共让人来否定他是毛的医生，以至把他暗杀。<br />
<br />
中共不是怕抹黑毛泽东，而是若「红太阳」的真面目暴露了，中共非法建立的政权就有危机了。而毛的家属之所以要花钱把张玉凤的回忆录买下来，是怕毛倒台了，直接影响到毛家人的待遇。<br />
<br />
什么「为人民服务」，什么「辉煌60年」，中国共产党怕的就是说真话，说实话。中共的历史是一部谎言史，「毛泽东思想研究室」是一个地地道道的谎言加工厂。△<br />
