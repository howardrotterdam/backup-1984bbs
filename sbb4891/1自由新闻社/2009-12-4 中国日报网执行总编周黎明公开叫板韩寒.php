																											<div id="postmessage_355297" class="t_msgfont">致韩寒的信：你掉进了《时代》设下的陷阱 2009-12-04<br />
<br />
中国日报网消息：英文《中国日报》12月4日报道：韩寒很聪明，但他接受《时代》采访却非明智之举。他应该把对付敏感话题的机智劲儿同时用于对付海外媒体。下面是本报编辑周黎明就此写给他的一封信。<br />
<br />
韩寒：你好！<br />
<br />
首先我说明，我不是你粉丝，所以别把这封信当作粉丝追星函扔进垃圾桶。<br />
<br />
我碰巧看到11月2号《时代周刊亚洲版》对你的采访，后来又读到你在《青年周末》上的回应。我深表同情。你挣脱了咱们自己的枷锁，但你掉进了西方媒体设下的陷阱。你以为《时代》的文章能让你的事业更上一曾楼，如同《纽约时报》把郭敬明捧成“中国最成功的作家”。但你从《时代》得到了什么呢？你变成了一个满嘴脏话、自我膨胀、阴柔女气、一切为了消费主义的小混混。而这恰好是你为郭敬明所做的画像――当然，郭敬明没有用脏话，所以，经过西方媒体调色后，他现在的形象至少比你更少污秽。<br />
<br />
其实，你应该从你的第一对手那里学两招，比如从《时代》文章中请人摘录一两句，掐掉上下文，扩散出去，比如用艾未未和张悦然的话，但让人错以为是《时代》的评语。西方媒体对于他们喜欢的东西从来就是忠实记录，如某些报刊那些被夸大数十倍的发行量；但若不符合他们的框架，那他们可要好好行使记者的权力，又是平衡，又是怀疑，又是质问。对于你的报道，反正我看完之后，对你的疑惑多于欣赏。<br />
<br />
郭敬明得到了“最成功”的桂冠，而你却成了“坏孩子”。这“坏孩子”在中文里可不带褒义。好在你不在乎。可是，说你是“无缘无故的叛逆”，那可真会极大地伤害你的自尊心。在西方媒体眼里，你若对上千万青年具有影响力，那算不了什么。即便你为莎朗・斯通辩护，可一脸严肃的西方记者会觉得那是少年的恶作剧。如果你能把高超的文采和诡辩术简化成一句话、一句口号，可以用来在公众场合呐喊或打横幅，那才是西方人看得懂的叛逆。诚然，西方记者都有中国本地的助理、研究员、翻译，但你写的东西并不适合翻译，你用你独特的言外之意，使得你既发表了声音，诱导了舆论，同时不至于被割喉。梁文道先生预测你是“未来的鲁迅”，但即便大胆如鲁迅，他在西方也几乎没有什么读者（况且他的作品有精彩的外语译本）。<br />
<br />
《时代》早在2000年就报道过你，那时，你跟卫慧、棉棉是打包在一起的。早知如此，你还不如改写言情小说，多点力比多，加上几张美图，文字让别人操刀都行了，保管比现在的销售量更高。<br />
<br />
若想赢得西方媒体的赞赏，你必须把自己包装成“异见分子”，你必须在谈话中不时插入“民主”、“人权”、“我们居住的星球”等字样，你必须动不动提一提千千万万仍在挨饿的人们（当然，那些人不在我国），并且配上沉重的表情，最好是眼中噙着一点泪花。除此，你还可以描述一下消失的冰山和受苦受难的北极熊。这样的话，国际奖项很快就会找上门来，拿到你手软。<br />
<br />
或者，你可以动用关系，请人把你的博客关闭一个月，让政府发动一场全民运动，对你进行声嘶力竭的批判。当然，最便捷的方法是把自己整到监狱里。只有这样，你才能摆脱《时代》授予你的“自愿的参与者”的称号――尽管你死活都不愿进入体制内的作协。作协恨你，体制咬牙切齿，郭敬明及其粉丝磨刀霍霍……可他们都不愿出来公开叫板，于是，你就成了一个唐吉珂德，无非是长矛换成了赛车。<br />
<br />
如今，《时代》把你唤作“一个阴柔中性、很会算计但没有理想的叛逆者”，这产生的杀伤力恐怕会超过某个学校授你三好学生、活雷锋等称号。这让我联想起网络上拼贴的你跟郭敬明的恋人照。前一阵你说你跟他的区别在于“性别”，按照《时代》的归类，你跟他岂非成了一对拉拉？算来算去，中国最男人的大概要算李宇春了，你看，只有春哥上了《时代》亚洲版的封面。下回你为《时代》拍美人照，先请教一下春哥。<br />
<br />
话说回来，你最好别再接受《时代》采访。他们死活要采访春哥，可春哥就不理他们，于是他们把她搁到了封面上。你敢于向奥巴马说不，你跟和菜头不同，你知道所谓的青年对话是对不出什么名堂的。和菜头如今成了怨妇，唠叨着奥巴马如何负了他，不就是人家没说“反对控制”，而只说了“支持言论自由”吗？和菜头那篇文章完全可以隆重推荐给美国的保守派媒体，天天在电台骂奥巴马的Rush Limbaugh一定欢天喜地。<br />
<br />
你对奥巴马说不，但心一软答应了《时代》。你把刚刚赢得的叛逆分给输掉了。如果《时代》他们找不到你，就只能依赖周围的人，而他们周围的人未必都是你的粉丝，但一般而言，你越不理会，他们对你越有敬意。另一种方法堪称王小峰法。《时代》把他作为中国代表归到2006年度人物“你”里面，王哥却编了一个故事，说这是他贿赂来的。那才叫叛逆，而且叛得颇有创意。你说你是被《时代》“下套儿”，你该向王哥讨教一下。王哥曾被路透下套，被硬生生打造成“异见分子”。他一气，把博客关了，躲了起来。路透找不着他，便按照自己的思路虚构了一篇报道。可你又不能重复王哥的创意，你必须想一个新招，才能证明你的编故事才智（他们对你写小说的才能似乎也有疑问）。咱们的体制是一个笨拙的巨人，你的灵巧很管用，但西方媒体是所谓的“神牛”，你得先学会斗牛。开赛车能镇住尖叫的女孩，但镇不住没有时尚细胞的记者。<br />
