													<h2>谷歌手机将可免费打电话 被指冲击通信业</h2>
						
						
																											<div id="postmessage_343937" class="t_msgfont"><a href="http://it.qq.com/a/20091126/000395.htm" target="_blank">http://it.qq.com/a/20091126/000395.htm</a><br />
<br />
导读：这是市场上首次出现单独一家公司可以提供全面服务的局面，谷歌将提供从用户手机软件到进行通话和网上冲浪等服务。<br />
<br />
腾讯科技讯 北京时间11月26日消息，据国外媒体报道，谷歌将通过推出智能手机，及提供包括首个无限自由通话的综合电话服务，来加速进军移动通信市场。<br />
<br />
这是市场上首次出现单独一家公司可以提供全面服务的局面，谷歌将提供从用户手机软件到进行通话和网上冲浪等服务。<br />
<br />
谷歌承诺其手机将是最先进的智能手机，配有大触摸屏，处理器运行速度将是苹果iPhone 3GS手机处理器运行速度的2倍。谷歌智能手机可能是首款可以运行谷歌新版Android软件的手机，谷歌智能手机还支持高速3D游戏，其性能不亚于众多游戏器。<br />
<br />
金融机构东北证券(Northeast Securities)分析师阿什科・库马尔(Ashok Kumar)表示，谷歌智能手机可能由第三方供应商生产，采用高通的处理芯片，生产商可能是台湾手机生产商宏达电。<br />
<br />
谷歌智能手机真正突破之处在于将Google Voice服务整合进智能手机。Google Voice是谷歌提供的电话服务，Google Voice为其美国用户提供一个免费电话号码，允许用户在美国国内无限制免费拨打电话，其国际电话费用一分钟也只需要几美分。Google Voice还可以通过语音识别技术，将语音邮件转成电子邮件，并可以自动屏蔽推销电话，还提供免费短信服务。<br />
<br />
谷歌在2周前收购了Gizmo5。Gizmo5己开发出将Google Voice与网络电话服务相连接的技术。谷歌现在可以提供全面、终端的电话服务。用户可以通过谷歌智能手机，与全世界任意地区的其他电话进行通话，而这些通话通常情况下是免费。<br />
<br />
库马尔称：“我们以前从未遇到过这种情况，单一的一家公司控制了全部服务，从操作系统到谷歌云服务，这将以前所未有的方式改变市场竞争和交易趋势。”<br />
<br />
谷歌拒绝对公司计划进行评论。<br />
<br />
谷歌进军手机市场的一个受害者可能是Skype。Skype软件通过网络为用户提供免费的声音和视频电话服务，Skype还为全球固话用户提供价格低廉的电话服务。如果谷歌可以成功将Google Voice服务与Skype及其他网络电话服务相连，谷歌可以通过提供免费长途电话和真正的电话号码，来吸引用户。<br />
<br />
谷歌面临的挑战之一是将电话与移动网络相连。如果将电话与移动网络相连，谷歌不但可以通过WiFi提供服务，还可以利用3G与互联网连接。这可能会引发一个问题，没有运营商愿意被排挤在有利可图的语音电话及短信业务之外，运营商不会愿意成为仅为谷歌服务提供数据的一个通道。<br />
<br />
谷歌直接面向消费者销售自己品牌的手机，并且让消费者可以使用谷歌服务，势必引起运营商的不满。这将导致谷歌手机的价格超过500英镑。运营商通过对智能手机给予大额补贴，以使消费者使用自己的绑定服务。<br />
<br />
谷歌面临的风险不仅来自于运营商，现在使用Android操作系统的诸如三星、摩托罗拉和索爱等其他手机厂商，也会逐渐放弃使用该操作系统。<br />
<br />
尽管Android操作系统自去年推出以来，日益受到手机厂商的欢迎，但其智能手机操作市场的占有率仍然不足4%，而且市场上还有其他免费手机操作系统，竞争对手很可能会转而使用其他手机操作系统。<br />
<br />
库马尔及其他专家预测，谷歌智能手机将在明年初在美国推出。(编译/观海)</div>

							
							
							
							
