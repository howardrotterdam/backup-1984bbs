<br />
　　他的话惹来人权组织的批评，但是图斯克从未收回自己的言论。<br />
<br />
　　赫尔辛基人权基金会的克拉多兹尼说：“动用任何强制性治疗都令人怀疑，因为这样做从来都不具有合理的必要性，而且人总是会造出一些政策制定者做梦也想不到的案子来。”克拉多兹尼说：“如果罪犯心理健康，我们要处罚他，如果他有病，我们就要治疗他，这是波兰法律所规定的。但是据我所知，新法律在娈童癖问题上是欧洲最严厉的。”<br />
<br />
　　新法律也增加了对强奸和乱伦罪的刑期。因为图斯克的公民纲领党拥有上议院多数席位，预计法律会获得批准。</div>

							
							
							
							
