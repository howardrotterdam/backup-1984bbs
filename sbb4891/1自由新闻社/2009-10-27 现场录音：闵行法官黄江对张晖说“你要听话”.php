二○○四年三月十九日<br />
最高人民法院 司法部<br />
关于规范法官和律师相互关系维护<br />
司法公正的若干规定<br />
为了加强对法官和律师在诉讼活动中的职业纪律约束，规范法官和律师的相互关系，维护司法公正，根据《中华人民共和国法官法》、《中华人民共和国律师法》等有关法律、法规，制定本规定。<br />
第一条 法官和律师在诉讼活动中应当忠实于宪法和法律，依法履行职责，共同维护法律尊严和司法权威。<br />
第二条 法官应当严格依法办案，不受当事人及其委托的律师利用各种关系、以不正当方式对案件审判进行的干涉或者施加的影响。<br />
律师在代理案件之前及其代理过程中，不得向当事人宣称自己与受理案件法院的法官具有亲朋、同学、师生、曾经同事等关系，并不得利用这种关系或者以法律禁止的其他形式干涉或者影响案件的审判。<br />
第三条 法官不得私自单方面会见当事人及其委托的律师。<br />
律师不得违反规定单方面会见法官。<br />
第四条 法官应当严格执行回避制度，如果与本案当事人委托的律师有亲朋、同学、师生、曾经同事等关系，可能影响案件公正处理的，应当自行申请回避，是否回避由本院院长或者审判委员会决定。<br />
律师因法定事由或者根据相关规定不得担任诉讼代理人或者辩护人的，应当谢绝当事人的委托，或者解除委托代理合同。<br />
第五条 法官应当严格执行公开审判制度，依法告知当事人及其委托的律师本案审判的相关情况，但是不得泄露审判秘密。<br />
律师不得以各种非法手段打听案情，不得违法误导当事人的诉讼行为。<br />
第六条 法官不得为当事人推荐、介绍律师作为其代理人、辩护人，或者暗示更换承办律师，或者为律师介绍代理、辩护等法律服务业务，并且不得违反规定向当事人及其委托的律师提供咨询意见或者法律意见。<br />
律师不得明示或者暗示法官为其介绍代理、辩护等法律服务业务。<br />
第七条　法官不得向当事人及其委托律师索取或者收取礼品、金钱、有价证券等；不得借婚丧喜庆事宜向律师索取或者收取礼品、礼金；不得接受当事人及其委托律师的宴请；不得要求或者接受当事人及其委托律师出资装修住宅、购买商品或者进行各种娱乐、旅游活动；不得要求当事人及其委托的律师报销任何费用；不得向当事人及其委托的律师借用交通工具、通讯工具或者其他物品。<br />
当事人委托的律师不得借法官或者其近亲属婚丧喜庆事宜馈赠礼品、金钱、有价证券等；不得向法官请客送礼、行贿或者指使、诱导当事人送礼、行贿；不得为法官装修住宅、购买商品或者出资邀请法官进行娱乐、旅游活动；不得为法官报销任何费用；不得向法官出借交通工具、通讯工具或者其他物品。<br />
第八条　法官不得要求或者暗示律师向当事人索取财物或者其他利益。<br />
当事人委托的律师不得假借法官的名义或者以联络、酬谢法官为由，向当事人索取财物或者其他利益。<br />
第九条　法官应当严格遵守法律规定的审理期限，合理安排审判事务，遵守开庭时间。<br />
律师应当严格遵守法律规定的提交诉讼文书的期限及其他相关程序性规定，遵守开庭时间。<br />
法官和律师均不得借故延迟开庭。法官确有正当理由不能按期开庭，或者律师确有正当理由不能按期出庭的，人民法院应当在不影响案件审理期限的情况下，另行安排开庭时间，并及时通知当事人及其委托的律师。<br />
第十条 法官在庭审过程中，应当严格按照法律规定的诉讼程序进行审判活动，尊重律师的执业权利，认真听取诉讼双方的意见。<br />
律师应当自觉遵守法庭规则，尊重法官权威，依法履行辩护、代理职责。<br />
第十一条　法官和律师在诉讼活动中应当严格遵守司法礼仪，保持良好的仪表，举止文明。<br />
第十二条 律师对于法官有违反本规定行为的，可以自行或者通过司法行政部门、律师协会向有关人民法院反映情况，或者署名举报，提出追究违纪法官党纪、政纪或者法律责任的意见。<br />
法官对于律师有违反本规定行为的，可以直接或者通过人民法院向有关司法行政部门、律师协会反映情况，或者提出给予行业处分、行政处罚直至追究法律责任的司法建议。<br />
第十三条 当事人、案外人发现法官或者律师有违反本规定行为的，可以向有关人民法院、司法行政部门、纪检监察部门、律师协会反映情况或者署名举报。<br />
第十四条 人民法院、司法行政部门、律师协会对于法官、律师违反本规定的，应当视其情节，按照有关法律、法规或者规定给予处理；构成犯罪的，依法追究刑事责任。<br />
第十五条 对法官和律师在案件执行过程中的纪律约束，按照本规定执行。<br />
对人民法院其他工作人员和律师辅助人员的纪律约束，参照本规定的有关内容执行。<br />
第十六条 本规定由最高人民法院、司法部负责解释。<br />
第十七条 本规定自公布之日起实施。<strong>第二条 法官应当严格依法办案，不受当事人及其委托的律师利用各种关系、以不正当方式对案件审判进行的干涉或者施加的影响。 律师在代理案件之前及其代理过程中，不得向当事人宣称自己与受理案件法院的法官具有亲朋、同学、师生、曾经同事等关系，并不得利用这种关系或者以法律禁止的其他形式干涉或者影响案件的审判。 第三条 法官不得私自单方面会见当事人及其委托的律师。</strong></div>

							
							
							
							
