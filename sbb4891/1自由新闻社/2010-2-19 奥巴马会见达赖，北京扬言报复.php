																											<div id="postmessage_484501" class="t_msgfont">Risking China’s Anger, Obama Meets With Dalai Lama<br />
<a href="http://www.nytimes.com/2010/02/19/world/asia/19prexy.html?ref=asia" target="_blank">http://www.nytimes.com/2010/02/1 ... prexy.html?ref=asia</a><br />
<img src="http://graphics8.nytimes.com/images/2010/02/18/us/18cnd-lamaspan/18cnd-lamaspan-articleLarge.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
WASHINGTON ― President Obama met on Thursday with the Dalai Lama, the exiled Tibetan spiritual leader, for a low-profile meeting at the White House that nonetheless raised the hackles of China.<br />
<br />
华盛顿-奥巴马总统周四在白宫低调地会见了流亡的西藏精神领袖达赖喇嘛，但这仍然激起了天朝的怨念。<br />
<br />
After the meeting with Mr. Obama, the Dalai Lama told reporters that the two men talked about democracy, freedom and human values. In a statement the White House said that Mr. Obama had expressed support for the preservation of Tibet’s “unique religious, cultural and linguistic identity and the protection of human rights for Tibetans in the People’s Republic of China.”<br />
与奥巴马会见后，达赖喇嘛告诉记者，两人谈论了民主，自由和人权价值。白宫声明说，奥巴马总统表达了对西藏保持独特的宗教、文化和语言身份，以及保护天朝境内藏人人权的支持。<br />
<br />
The meeting ― which the White House initially put off last year so as not to raise tensions with China before Mr. Obama’s trip to Beijing in November ― is the latest twist in the more muscular position that the Obama administration has been adopting with China, after a year in which China has struck a firm stance with the United States on a number of issues.<br />
<br />
Last month, the administration announced a $6 billion arms sales package to Taiwan, a move that infuriated Beijing because it considers Taiwan a renegade province. The meeting Thursday between Mr. Obama and Tenzin Gyatso, the 14th Dalai Lama, was not going over well with Chinese officials either. China regards the Dalai Lama as an advocate of Tibetan independence.<br />
<br />
Chinese officials demanded―in vain―that the United States reverse its “wrong decision” for Mr. Obama to meet with the Dalai Lama. And while the White House has taken pains to avoid the appearance that this is a meeting between two heads of state―Thursday’s meeting, for instance, took place in the White House Map Room, and not the Oval Office―the administration also announced that Secretary of State Hillary Rodham Clinton would also meet with the Dalai Lama later at the State Department.<br />
<br />
天朝官员徒劳地要求美帝改正错误，取消奥巴马与达赖的会见。尽管白宫努力避免给外界造成两国元首会见的印象――例如周四的会议不在总统办公室，而在白宫地图资料室――但是政府行政部门仍然公布消息说，国务卿希拉里克林顿也将随后在政府部门会见达赖喇嘛。<br />
<br />
“The Dalai Lama is a Nobel Peace Price laureate, internationally revered religious and cultural leader and the secretary will meet him in this capacity as recent Secretaries of State have done,” said a State Department spokesman, Mark Toner.<br />
<br />
美帝政府部门发言人Mark Toner说：“达赖喇嘛是诺贝尔和平奖得主，是国际上受人尊崇的宗教和文化领袖。国务卿将在此范围内与达赖喇嘛会见，如同最近历届国务卿一样。”<br />
<br />
White House officials had said they would keep the visit low-key, releasing a photo after the meeting between the Dalai Lama and Mr. Obama. But there was no joint public appearance.<br />
<br />
白宫官员说，他们的会见将会保持低调，在会议后将会发布两人见面的合影，但是不会一起公开露面。<br />
<br />
American presidents, in deference to China, usually do not meet publicly with the Tibetan spiritual leader. President George W. Bush broke with that tradition back in 2007, though, when Mr. Bush attended a ceremony in the Capitol Rotunda in which Congress bestowed upon the Dalai Lama its highest civilian honor, the Congressional Gold Medal.<br />
<br />
美帝历届总统，畏及天朝威严，历来不敢与西藏精神领袖公开见面，但在2007年乔治布什总统打破了传统，在国会大厅出席了国会授予达赖喇嘛最高公民荣誉“国会金奖”的仪式。<br />
<br />
The Dalai Lama has lived in exile in India since the Chinese military crushed an uprising in his homeland in 1959, and is revered as the spiritual leader of Tibetan Buddhists. He has been pressing to return to China to advocate for greater cultural and religious freedoms for his followers.<br />
<br />
达赖喇嘛自从1959年天朝大军镇压了西藏起义后一直流亡在印度，直到现在作为西藏佛教徒的精神领袖受到崇敬。他一直努力回到天朝为他的信徒争取文化和宗教自由。<br />
<br />
China experts said that Chinese President Hu Jintao might retaliate for Mr. Obama’s meeting with the Dalai Lama by canceling his planned visit to Washington in April.<br />
<br />
中国专家说，胡帝有可能取消四月份的访美计划，来报复美帝与达赖的会面。</div>

							
							
							
							
