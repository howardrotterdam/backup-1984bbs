<img src="http://imgur.com/4283a.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
奥巴马周四与达赖喇嘛会晤后，达赖喇嘛在白宫门外向媒体表示，他对会晤非常满意，并说奥巴马对他非常的支持。(Getty Images) <br />
<br />
【大叽歪2月19日讯】美国总统奥巴马周四（18日）在白宫与西藏流亡精神领袖达赖喇嘛举行了一个多小时的闭门会晤。达赖喇嘛表示，他与奥巴马总统谈及如何促进和平与宗教和谐等问题。奥巴马总统表示支持“维护西藏独特的宗教、文化和语言特征，并维护在中国的藏人的人权。”<br />
<br />
据BBC报道，奥巴马周四与达赖喇嘛会晤后，达赖喇嘛在白宫门外向媒体表示，他对会晤非常满意，并说奥巴马对他非常的支持。他说，他与奥巴马总统谈及了如何促进和平与宗教和谐等问题。<br />
<br />
达赖喇嘛说，他向奥巴马提及他的两大承诺，一是促进人的价值，以便带来一个和平的世界、和平的家庭以及和平的个人。二是在保护一种传统，一种宗教的同时，也要尊重所有主要的宗教传统。这是在不同宗教传统中构建真正和谐的基础。<br />
<br />
白宫发言人吉布斯说，奥巴马总统在会晤开始时表示，他支持“维护西藏独特的宗教、文化和语言特征，并维护在中国的藏人的人权。”他还说，奥巴马称赞了达赖喇嘛推行的“中间路线”、他的非暴力承诺以及他争取与中国政府对话的努力。<br />
<br />
奥巴马强调，他一直鼓励达赖喇嘛与北京通过直接对话的方式解决分歧。吉布斯还说，奥巴马和达赖喇嘛都同意美中两国保持积极与合作关系的重要性。<br />
<br />
为了避免过多激怒中国，本次会晤安排非常低调，整个过程不许媒体采访。会面地点设在白宫的地图室，而不是象征总统权力中心的椭圆形办公室。<br />
<br />
达赖喇嘛这次来访正值藏历新年，也是中美关系趋于紧张的时候。最近几个月，美中关系因为美国向台湾出售武器、美国公司谷歌威胁退出中国以及两国之间的贸易摩擦而趋于紧张。而达赖喇嘛与奥巴马总统的会晤使业已紧张的中美关系再次受到考验。 <br />
<br />
达赖喇嘛17日在抵达下榻的华盛顿柏悦大酒店的时候，受到藏人的热烈欢迎。达赖喇嘛在华盛顿的活动，除了和当地藏族同胞共度藏历新年外，19日还将在国会图书馆发表演说。</div>

							
							
							
							
