													<h2>金正日再访中国或谋“一箭三雕”</h2>
						
						
																											<div id="postmessage_800033" class="t_msgfont">①推进局势变化可能性<br />
<br />
②在党代表会议前夕为金正银铺路<br />
<br />
③解开5月访中时的误会并寻求军事援助<br />
<br />
8月26日北韩国防委员长金正日访问中国。这是自今年5月访问中国北京后，时隔仅三个月的再次访问。金正日上台后共六次访问中国，但从未像今年一样一年内访问两次。<br />
<br />
高层脱北者A某对此表示：“金正日可能认为此次访问的出发点与过去的访中有所区别。”如果金正日从一开始就计划探寻金日成主席的痕迹，那么此次访中从表面上看是访中，实际内容则是“探寻金日成革命遗址”。如果是这样，是否在3个月前访问过中国，完全不重要。<br />
<br />
韩国统一部一位官员称：“距离劳动党代表会议只剩下2周，要注意到这一点。”金正银可能会通过时隔44年举行的此次会议，正式或非正式地获得接班人地位。经过一年多的密集宣传、煽动，北韩政府全方位地“创造”了金正银的业绩，但“金日成王朝嫡系血统”问题并未彻底解决。<br />
<br />
安全部门一位官员称：“金正日的有力接班人、三子金正银的母亲是旅日侨胞，在北韩被当作‘二等公民’，因此具有很难成为金日成王朝嫡系血统的天生局限性。眼下已经没有太多时间，金正日父子似乎认为‘要想避开血统争议，只有探寻金日成革命遗址这一个办法’。”<br />
<br />
《劳动新闻》等北韩国营媒体当天上午一致报道称：“在朝鲜劳动党代表会议前夕，各市、区、郡党代表会议已于近日举行。”据分析，这些报道也与此次访中有着密切联系。北韩先举行市、区、郡（基础团体）党代表会议，然后举行道、直辖市党代表会议，最后于下月初举行全国党代表会议。<br />
<br />
统一部一位官员称：“当天北韩媒体报道的意义在于向国内外宣布了‘党代表会议已正式启动’。在金正日赴中国探寻金日成革命遗址的第一天出现这些报道，绝非偶然。”<br />
<br />
政府内部有人认为：“就算是以‘圣地巡礼’为目的，金正日既然到了中国，就不太可能谁也不见。此次可能会与重量级人物会面，试图解决今年5月访中时没有落实的问题。”因此有分析认为，金正日此次访中可能带着大幅改变美北、南北关系的重大决心。<br />
<br />
也有分析认为，北中首脑在今年5月见面时在多个问题上存在分歧，有必要就此进行补救。北韩内部消息人士称，金正日5月访中时还曾向中国国家主席胡锦涛寻求无偿援助中国新型“歼-10”战斗机。据说，中国方面以“如果北韩遭到攻击，我们会出面保护，北韩不必要拥有最新型武器”为由，表示拒绝。<br />
<br />
随着韩美两国自上月起针对北韩攻击天安舰一事实施东海、西海大规模联合军事演习，当时空手而归的金正日感到不安，可能认为有必要依赖于中国的军事力量。中国方面同样对韩美联合军演感到不满，因此，如果金正日再次建议军事援助，也有可能改变态度。<br />
<br />
外交消息人士称，金正日对胡锦涛提到的“加强内政外交战略沟通”及中国国务院总理温家宝提到的“愿意介绍中国的改革开放经验”深感不满。原因是金正日最反感“改革开放”和“干涉内政”。京畿大学教授南柱洪表示：“金正日和胡锦涛在今年5月进行的是一次被特殊关系所掩盖的非正常首脑会谈，北中双方都很不满。”也就是说，金正日此次神秘中国行，含有解开上次访中时北中间产生的误会这一意图。 <br />
