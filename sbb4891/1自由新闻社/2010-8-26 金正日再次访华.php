<font color="DarkSlateGray"><strong><font size="3">更新时间 2010年 8月 27日, 格林尼治标准时间08:29</font></strong></font><br />
<br />
<strong><font size="6"><font color="Black">“胡锦涛将在长春会晤金正日”</font></font></strong><br />
<br />
　　<strong>中国消息来源证实，中国国家主席胡锦涛星期六（8月28日）将在吉林省会长春市会晤朝鲜领导人金正日。</strong><br />
<br />
　　<img src="http://j.imagehost.org/0488/0_8.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　<font color="DarkRed">金正日和胡锦涛5月曾在北京会晤（档案图片）</font><br />
<br />
　　长春高层可靠消息人士透露，金正日星期五（8月27日）已经抵达长春，而胡锦涛则将在星期六到达。<br />
<br />
　　韩国媒体报道称，金正日一行星期五上午乘轿车从吉林市抵达长春，并说他们下榻在长春的南湖宾馆。<br />
<br />
　　BBC中文网致电南湖宾馆时被告知，该宾馆一直到9月5日之前都不会接受订房。<br />
<br />
　　法新社说，酒店员工透露该处在举行“国家级会见”。<br />
<br />
　　<strong>目的</strong><br />
<br />
　　这是金正日三个月来第二次访问中国，外界对他此行的目的猜测纷纭。<br />
<br />
　　很多分析人士认为他此次访问中国很大可能是希望中国对遭受严重水灾的朝鲜施以援手。<br />
<br />
　　虽然韩国仍有民间团体继续对朝鲜的粮食援助，但韩国政府此前已经表态称不会继续援助朝鲜。<br />
<br />
　　韩国消息来源称，普遍被视为中国下一任领导人的习近平星期四与金正日共同下榻在吉林市的雾凇酒店，而且与金正日共进早餐。<br />
<br />
　　同时韩国媒体报道说，已经被内定为金正日接班人的他的儿子金正银此次也随他访问中国，并猜测金正日可能希望借这次访问为儿子掌权铺路。<br />
<br />
　　<strong>卡特</strong><br />
<br />
　　与此同时，美国前总统卡特的发言人在中国时间星期五上午突然宣布卡特赢得朝鲜同意赦免因非法入境而被判八年刑期的美国人戈麦斯。<br />
<br />
　　这名发言人透露，戈麦斯的赦免是金正日批准的。她还透露，朝鲜向卡特表示愿意重返六方会谈。<br />
<br />
　　而卡特星期三下午抵达平壤，金正日同日晚些时候乘其专列出发前往中国，曾有报道称卡特因未能与金正日会晤决定延长在朝鲜的逗留。<br />
<br />
　　外界难以判断在中国的金正日突然赦免戈麦斯是否与他与习近平的会晤有关。<br />
<br />
　　不过分析人士普遍认为，中国应该会利用朝鲜需求援助的机会争取说服朝鲜重返六方会谈。<br />
