													<h2>南方周末：打黑风暴中的重庆政法界</h2>
						
						
																											<div id="postmessage_358125" class="t_msgfont"><a href="http://www.infzm.com/content/38313" target="_blank">http://www.infzm.com/content/38313</a><br />
打黑风暴中的重庆政法界<br />
作者: 南方周末记者 赵蕾 发自重庆 2009-12-02 22:39:15 来源:南方周末<br />
&quot;■重庆打黑，两百多专案组在高度保密中运转，谁泄密就可能被捕。<br />
专案组请了律师来参谋，要求他们吃住都在专案组。不少涉黑人员被以代号关押，代号不解密，律师也见不到涉案当事人。<br />
■在有的涉黑案庭审时，检察官可以准备多达50万字的公诉预案、几大叠的证据材料，而律师由于不被允许复印审讯笔录等证据，“只能到庭上去听公诉人怎么说”。<br />
■这是被官方认为自1983年“严打”以来最大规模的治安整治，一时间看守所、拘留所爆满。&quot;<br />
&quot;有人认为重庆打黑很坚决，也有人认为打黑过程中存在一些程序问题，“担心负面问题冲淡了正面作用”。&quot;<br />
&quot; 而重庆官方认为，大规模打黑净化了社会治安环境。&quot;<br />
2009年12月1日，陈知益、邓宇平等26人涉黑团伙在重庆市第二中级人民法院公开开庭审理，法警和武警押解陈知益（01号）、邓宇平（02号）等26人涉黑团伙成员进入法院。　CFP/图<br />
重庆官方称，11月28日，重庆市高级法院执行局原局长乌小青在重庆市第二看守所自缢身亡。<br />
乌小青是今年6月开始的重庆打黑风暴中落马的官员之一。他涉嫌受贿和巨额财产来源不明罪。“不是他的死，我们都不知道他关在哪里，案子进行到什么阶段。”重庆市法院系统一位处级官员告诉南方周末记者。<br />
此前连续传出另外两位涉黑官员在接受调查或审讯时发生非常状况――重庆市公安局原副局长彭长健在“双规”期间心脏病突发身亡，重庆市公安局交通管理局原局长陈洪刚在接受审讯时试图撞墙自杀，但都没有得到官方的证实。<br />
乌小青身后留下一封遗书和坊间诸多猜测，令雾都打黑再罩云雾。<br />
非常侦查<br />
&quot;“进了专案组，像进了情报局。”一位专案组民警的亲戚说，更传一位办案人员因涉嫌泄密被抓。律师也被请来献策，和警察、检察官一样，吃住都在专案组。&quot;<br />
11月中旬，重庆市公安局一位负责人告诉南方周末记者，专案组将争取在11月底将所有涉黑案侦结，移送检察院审查起诉。<br />
另有消息说，上周末有关方面已经决定，另一与乌小青案相关、重庆市高级法院原副院长张|的案件，将交由异地法院审理。<br />
此时正值涉黑案件司法审查的关键阶段，不容有失。<br />
官方所说的两百多个专案组分布在重庆主城区和下辖区县。各专案组由武警、公安局警察、检察官和驻专案组律师组成。进入专案组的人员，都签了保密协议：其中，对他们原单位的领导，无论是谁问及打黑除恶相关情况，只要对方未参与打黑除恶工作，一律必须拒绝回答。“进了专案组，像进了情报局。”一位专案组民警的亲戚说，“专案组的人其实也很不安全。”乌小青自杀两天前，专案组曾召开紧急会议，会上传出消息，一位办案人员因涉嫌泄密被抓。<br />
这几天，专案组内部对打黑话题更加讳莫如深。<br />
进入10月，虽不见警方频频出动密集抓人，打黑形势没有前几个月那么惊心动魄，却没有丝毫松缓。“前面几个月是怕跑了人，现在是要把证据做实。”这位民警的亲戚说。<br />
据10月7日出版的《重庆晚报》报道，每天工作16小时以上，平均每天仅休息3至4个小时，最多休息5小时，是专案组的工作常态。<br />
一位民警说，审讯陈明亮、黎强等黑社会性质组织首犯嫌疑人时，更是通宵达旦。<br />
检察官也是这个高强度运转的办案机器中的重要部件。重庆市检察系统出动了近半数检察官，同步介入涉黑案件的侦查，打破了以往刑事案件中，由公安主要承担侦查工作的模式。<br />
重庆市检察院宣传处人士向南方周末记者提供了一组数据：检察机关在不到三个月的时间内向公安机关提出了一千余条补充侦查。其中，谢才萍等人涉黑案提出三百余条补充侦查意见，陈明亮等人涉黑案提出一百余条补充侦查意见。“公诉人员都是院里和专案组两头跑，特别忙。”重庆市检察院宣传处的小秦告诉南方周末记者，她也联系不上公诉处的负责人，无法安排采访。<br />
加入打黑战役中的，还有不远千里来到重庆帮助打黑的辽宁律师王蕴采。<br />
王蕴采是重庆市公安局长、打黑中坚人物王立军的老朋友。2003年前后辽宁打黑时，王立军邀请她进驻专案组。今年8月，她和其他几位律师来到重庆，再次为王立军打黑参谋。<br />
11月23日，重庆市打黑专案组的法律顾问王蕴采告诉南方周末记者，这几天专案组取得两个关键性的突破，由于有保密规定，不能透露详情。<br />
这些公安局聘请的律师，和警察、检察官一样，吃住都在专案组。王蕴采说，每天也看案卷到十一二点，“和辩护律师不同，我们主要是为案件侦破质量把关”。<br />
律师进入专案组为打黑侦查献策，为王立军在辽宁打黑时首创，这次被复制到重庆打黑上。王蕴采说，律师的加入，能为侦查机关提供明确的侦查方向，避免浪费司法资源。<br />
除了专业的法律人士外，重庆当局动用了更多的社会资源服务于打黑。<br />
6月底，重庆市公安局就向20万市民发出号召，请他们举报身边的黑恶势力。<br />
从9月开始，重庆市各监所开展了“坦检”工作：对新收押收容的涉黑人员，监所人员必须进行深挖，最好能挖出漏罪；坦白检举涉黑涉恶犯罪事实的在押罪犯和劳教人员，将给予奖励。<br />
高校研究团队“贝卡里亚青年队”认为，重庆打黑侦查阶段具备“特有的高效率和行政机关强大的主导”。据他们观察，“这无疑与正当程序模式大相径庭，却取得了较好的社会效果。”<br />
非常代号<br />
&quot;涉黑被抓人员的家属称，常常不知家人被关在何处，因为家人被以代号关押，专案组也没有固定办案地点。律师说：只要是以代号关押的，律师就见不到，除非代号被解密，这在以前没有过。&quot;<br />
9月中旬，吴女士的丈夫因涉黑被警方带走。两个月来，她已经逐渐接受了短期内见不到她丈夫的现实。乌小青自杀的消息，触动了她脆弱的神经，“啊，出人命了！”<br />
在最初一个月里，她问了重庆市几个公安分局，“都说自己不是专案组的人，也不知道专案组在哪办公。”<br />
坊间有消息说，专案组包下了几家宾馆和郊区僻静的农家乐，作为办案地点。一位涉黑被抓人员的家属说，她每次送东西，都放在某家宾馆门口，专案组的人出来收。<br />
因为专案组没有固定的办公地点，律师韦峰不知道律师委托文书该往哪里送。<br />
“律师连个（跟公安或者检察院）接头的地方都没有，怎么向当事人家属交代？”最后，他只能请一位以往打过交道的检察官收下。<br />
吴女士也是托熟人找到某个分局内勤部门的人员，帮她把要送进去的衣物交给专案组。<br />
除了专案组不对外公开外，涉黑人员被逮捕前关在哪里，也是个谜。韦峰说，他曾看到一份讯问笔录上面，关押地点一栏赫然写着“临时羁押场所”。<br />
“只听到轰隆隆的响声，可能是在机场附近。”一位被请去协助调查的人士说，“一直戴着头套，在市区转了很久，最后就到了那个地方。”<br />
到上个月中旬，重庆市共抓获涉黑涉恶人员2905人。不只一位政法界人士告诉记者，如果把被请去协助调查、抓后被取保候审或者送去劳动教养的人加上，就不只这个数，这对重庆市已有的羁押场所来说，确实压力不小。因此使用临时关押场所，并不让人感到意外。<br />
吴女士的丈夫因三年前为一涉黑老板提供过中介服务牵涉其中。她说，在看守所、律师事务所，她已经碰见了好几个涉黑人员的家属，“评估师、会计师都有”。<br />
重庆市检察院提供的数据显示，截至10月18日，重庆市检察系统以涉黑罪名批准逮捕355人，以涉黑罪名起诉的有136人。尽管被抓的人超过一半最终都和“黑社会”罪名无关，但多数人最初被带走的理由都一样：涉黑。<br />
只要涉黑，就会遭遇更严密的监管和保密措施。<br />
丈夫被带走将近40天后，吴女士收到了重庆市九龙坡公安分局的逮捕通知书，告知她丈夫关在九龙坡区看守所。在看守所的接待室里，吴女士碰见了几位涉黑人员的家属。因为说不出代号，看守人员拒绝为他们转交衣物。<br />
她随即想起了之前公安局内勤部门代交衣物留下来的一张收据。“上面写的交款人没有姓名，只有四个数字，我当时就记下了。”她说。<br />
吴女士报出了那四位数字，看守人员查对后，把东西接了过去――尽管无法确定代号对应的是不是她丈夫，东西是否送到了他手里――她还是觉得庆幸，“至少能把东西送进去”。<br />
“只要是以代号关押的，律师就见不到。只有代号被解密，看守所以人名管理时，才能见到人。”律师韦峰说。在以往办理刑事案件的经历中，他没有遇到过这种情况。<br />
谁有解密代号的权力，也没有清晰的说法。9月，重庆市公、检、法、司、国安五家联合发文，规定黑社会性质组织犯罪嫌疑人的律师，必须经过公安局的批准，才能会见在押嫌疑人。据记者了解，通常情况下，代号被解密时，案子已经到了检察院审查起诉阶段。<br />
11月中旬，重庆律协召开的一次会议上，有律师提出，司法局和律协应该为律师在打黑案件中的权利保障说话，马上被另一位律师打断，“什么时候都能谈律师权利，这个时候不能！”<br />
这位律师直陈“形势”：司法局本来地位就弱，现在司法局长被抓了，二级局劳教局的局长也被抓了，这个时候能保太平无事就已经谢天谢地了，你还让它跟谁较劲呢？<br />
身为重庆打黑专案组头牌法律顾问的王蕴采告诉记者，律师会见难不是重庆才有。她同时表示，侦查中的这些问题，没有反映到专案组来。<br />
一位重庆市政法委的人士说，涉黑案件利害关系盘根错节，律师提前介入对侦破案件不一定有利，面对这些重大、复杂案件，有时为了案件能顺利推进，侦查部门可能会做些变通。<br />
涉黑案子不好办，出现了很多新情况，是公安局和检方一致的说法。这位政法委人士坚称，只要庭审是公开进行的，前面诉讼程序的瑕疵不可能影响案件质量。<br />
非常交锋<br />
&quot;韦峰将所有功劳归于第一个登场的辩护律师赵长青，“没有他，后面的律师都会垮掉”，“43个律师相互间未经任何沟通，全部否认了检方对各自当事人的黑社会犯罪指控”。&quot;<br />
形势决定了庭审是涉黑嫌疑人和律师们与超级侦查团交锋的惟一机会。<br />
黎强的辩护律师赵长青在开庭前，只拿到了几页纸，他的对手、公诉人则准备了多达50万字的公诉预案、几大叠的证据材料。“只能到庭上去听公诉人怎么说。”赵长青说。<br />
即便如此，黎强系列案的庭审取得了令人意外的效果，没有出现谢才萍案中“一边倒”的局面，有网友戏称谢案“整场庭审就是公诉人在发表打黑宣言”。“黎强那场，像正儿八经的庭审。”重庆市一位中级法院法官说。他认为，这样的案件非常考验律师的胆略。<br />
韦峰是这个系列案第23个上场的律师，为重庆市巴南区公路运输管理所所长蒋洪辩护，蒋被指控涉嫌包庇黑社会性质组织罪。他的表现也为媒体所称道。<br />
“赵老师给我们定好了调子，没有他，后面的律师都会垮掉。”韦峰将所有功劳归于第一个登场的赵长青，“43个律师相互间未经任何沟通，全部否认了检方对各自当事人的黑社会犯罪指控。”<br />
在这种氛围的影响下，有个律师甚至在庭上慷慨陈词，大呼“打黑不是黑打！”令一旁听者印象深刻。<br />
黎强案开庭前，重庆市高院收到了数十家媒体的旁听申请。因为座位有限，如何确定哪些媒体可以进来，他们费尽脑筋。最后，他们决定，让全部申请的媒体都进来，包括境外媒体。“事实证明效果很好。”重庆市高院的一位人士说。周立太发现，来旁听庭审的人大代表和政协委员，一听完公诉人宣读完公诉词就走了，庭审结束后又突然出现，对案件发表意见。<br />
“我是整个庭审都听完了。”全国人大代表、重庆市开县福城村党总支书记张国安受邀旁听了开县李志刚涉黑案的庭审。他说，代表和委员们大多不懂法律，也只是去看看，“一方是不是压过了另一方，有没有不让他（律师）讲话。”<br />
外界最为关注的文强案，或将吸引更多的媒体。有记者建议重庆高院，可以考虑在审判庭外设置大屏幕，同步直播。<br />
据了解黎强案，庭审结束后，检方专门开会总结了这场庭审的得失。重庆市检察院宣传处的有关人士则对南方周末记者表示，总结是打黑以来检察院的工作常态。<br />
不过，黎强案的庭审效果没有让赵长青和韦峰感到振奋。<br />
目前，他们都推掉了其他涉黑案件的委托。“已经签约的，我都把律师费退了。”韦峰说，“手头上只有两件涉黑的案件，因为是亲友所托，没能推掉。”<br />
有意聘请赵长青当辩护律师的涉黑人员家属，更是络绎不绝。赵长青说，他不想接了，这种案子不好说。一周前，他刚刚接到法院通知，因为事实复杂难点颇多，黎强案将延期审理。<br />
重庆市司法局在9月8日专门发文，要求办理涉黑案件的律师，需要讲政治、顾大局、守纪律，要求相关律师不得接受媒体采访，不得泄密，对苗头性问题要及时报告，等等。多项限制让很多重庆当地律师不愿接此类案件，更有律师表示，即使没这些限制，他也不想接，因为这类案件发挥空间太小。<br />
记者们都在打听文强的辩护律师到底会是谁，来自重庆市律协的消息称，文强家属请的是北京的律师。在接下来即将进入法院审理阶段的陈明亮、王天伦等案件，将有来自北京的律师亮相。北京律师宣东就是其中一个。他已经准备在庭审中结合他当事人的案情，有针对性地发表对当前黑社会性质犯罪的看法。<br />
在2003年轰动全国的辽宁刘涌涉黑案中，北京律师田文昌曾因为刘涌辩护而名声大振，同时也遭到公众的唾骂和舆论的重压。在那之后，他几乎不接黑社会性质犯罪的案件。<br />
六年后，为黎强辩护的赵长青依旧难逃骂声。记者问及他的感受时，赵长青以一句“他们不懂法律，不用跟他们计较”轻轻带过。他相信，日后开庭的涉黑案件的律师，同样会站在法律的立场上说话。<br />
非常“杂音”<br />
&quot;重庆律师周立太认为：“有关领导的初衷是好的，不过，我担心负面效果冲淡了正面作用。”&quot;<br />
几场庭审过后，打黑中最刺耳的“杂音”开始出现。<br />
在李志刚涉黑系列案中为李利辩护的周立太律师，公开表达了他对重庆打黑扩大化的忧虑。据记者了解，在公检法机关内部，也有部分人赞同他的看法。<br />
韦峰担任法律顾问的一家公司，其中有一个股东被认定涉黑，其他三个股东也被警方带走。“这些股东的哥哥、姐姐、嫂子、妹妹，公司的出纳、会计、前台接待、司机都被带走了。”韦峰说，“我想下一步是不是该来找我谈话了。”“全家都快被抓光了，总得有人帮他们说话。”赵长青说，最后促使自己接手黎强案，主要出于对黎家全家处境的同情。<br />
据赵长青观察说，重庆已经多年没有出现全城搜捕的行动。从去年以来，这样的行动已经有好几次。去年9月那次，被官方认为是1983年严打以来最大规模的治安整治。各项打击指标创重庆市单月历史最高，一时间看守所、拘留所爆满。<br />
一方面，周立太明显感到这两年公安出警比以前快了，治安管理也比以前更有效。他说，这是铁腕打黑带来的正面效果。10月29日，重庆市政法委书记刘光磊告诉外界，重庆大规模打黑以来，社会治安环境得到净化，主城区110接警量减少了40％，民众安全感明显上升。<br />
当下的打黑形势，令天涯社区重庆版的一位网友联想起北京市二中院审理的臧天朔案。在那个案件中，检方指控臧天朔授意指挥了廊坊火车站百人持械斗欧，造成1死2伤，认为他构成聚众斗殴罪。<br />
这位网友问道：“如果臧天朔案发生在眼下的重庆，会不会被定为黑社会？”<br />
更多的重庆市民关心的是，文强会不会在庭审中曝出更高级别的官员。他们希望政府能真正做到除恶务尽。<br />
他们中有人认为，为了打黑，采用非常手段并无不可，&quot;矫枉肯定必须过正！&quot;重庆市民、退休科研人员徐宗俦说。“有关领导的初衷是好的，不过，我担心负面效果冲淡了正面作用。”周立太说，像大版面登载涉黑人员照片、大肆渲染“包养”、“情妇”等话题，就已偏离了法治的基本价值。<br />
乌小青自杀消息一出，引发了重庆打黑以来外界对官方的首次普遍质疑。<br />
有人认为，这是反思重庆打黑得失的最好契机。不过，同时也有重庆网友称这只是打黑的又一小杂音，不必理会。<br />
多位重庆市政法界人士也对南方周末记者表示，此次事故不会影响打黑的整体步调，“不可能有影响，还是该干嘛干嘛。”</div>

							
							
							
							
