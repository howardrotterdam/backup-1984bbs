　　在监控录像中，乌小顺看到了他哥哥生命里的最后片断：<br />
<br />
　　11月28日12点31分前，还能看到我哥在监舍里来回走动，12点31分之后就看不到了，录像带里只有静止画面，画面中无人员活动。<br />
<br />
　　41分钟后，也就是13点12分，录像里又出现了人影，原来是同监舍的4个人在打铃（监舍里配备有铃，有事就打铃通知监管人员），打了三次，前后共约3分多钟，没见人来。<br />
<br />
　　接着，同监舍的人把我哥哥放下来，做了一些急救动作。又过了一会，来了一个医生，后来得知是看守所聘请的医生，据说没有行医资格，他拉着我哥哥的手臂，也做了几下动作，同监舍的人也在帮忙。<br />
<br />
　　再过了一会，救护人员到了，是红十字医院第一分院，在看守所内，把我哥哥抬上车，前往第一分院抢救，这个时候他应该还有生命迹象。<br />
<br />
　　在红十字医院第一分院抢救一段时间后，乌小青被送往相隔40多分钟车程的红十字医院抢救。15点40分，院方宣布抢救无效死亡。<br />
<br />
　　八个小时后，乌家人才得知乌小青死亡的消息。乌小顺告诉记者，看完监控录像后，乌家人当场提出，为什么院方宣布死亡的第一时间没有通知他们？<br />
<br />
　　涉嫌受贿罪和巨额财产来源不明罪的重庆市高级法院原执行局局长乌小青，在看守所里死了。重庆市官方称，这是自杀。但乌小青的亲友与家属称：乌小青该交代的已经交代了，赃款也吐出来了，他也知道自己不可能被判死刑，案子正将进入审判阶段。这本应是最不可能死的时候，他为什么却要自杀？<br />
<br />
　　另一名看了乌小青死前监控录像的家属回忆，录像带中关键的41分钟，出现了谜一样的静止画面，画面中无人员活动……<br />
<br />
　　12月1日上午8：40，重庆市石桥铺殡仪馆高档火化间1号厅。重庆市高级法院执行局原局长乌小青在此化为几缕青烟。<br />
<br />
　　乌小青的妻子晏世平、儿子乌江、弟弟乌小顺等乌小青生前至亲，在1号厅的等候室里，隔着玻璃，目送他离开了这个世界。<br />
<br />
　　殡仪馆工作人员回忆，这天清晨5点左右，是两名警察来殡仪馆总服务台办妥了火化的所有手续。<br />
<br />
　　据官方发布的消息，四天前的11月28日中午12点31分，乌小青趁同监舍被羁押人员熟睡时，避开重庆市第二看守所第五监舍的监控摄像头，用一条棉毛裤裤腰带结束了57岁的生命。<br />
<br />
　　乌小青生前涉嫌受贿罪和巨额财产来源不明罪，被疑在执行局长任上利用手中权力，在一宗拍卖案中与涉黑组织勾结牟取利益。乌小青于今年7月被重庆市第一检察院批准逮捕，重庆检方曾称：1998年到2008年，乌小青先后索取、收受多人贿赂共计人民币357.5万元、港币10万元，涉嫌受贿犯罪；同时还查明，乌另有518万元人民币不能说明合法来源，涉嫌巨额财产来源不明犯罪。<br />
<br />
　　乌小青死前，此案正在审查起诉阶段。在此前后，引全国瞩目的重庆打黑多起大案也正在进入庭审。<br />
<br />
　　针对乌小青之死，重庆市公安局一位主要负责人告诉记者，审查与抓捕乌小青并非如外界所认为的属此轮打黑风暴中的案子，而是早在打黑之前，乌小青的问题已被发现，主要由纪委有关部门以及重庆市检察院负责乌小青案的调查。<br />
<br />
　　但也有网络舆论认为乌小青之死与重庆打黑不可能无关，不少网民质疑乌小青之死的蹊跷。<br />
<br />
　　对此，乌小青一位昔日关系密切的同事告诉记者，他在最不该也最不可能死的时候死了，“最难熬的时候应该过去了（注，指刚被捕的招供阶段）”。<br />
<br />
　　第二天下午，重庆市政府新闻办向外界通报了乌小青自杀的消息。消息全文为：11月28日12时31分，被羁押在重庆市第二看守所的犯罪嫌疑人乌小青留下遗书后，趁同监舍被羁押人员午睡之机，避开监控录像，用棉毛裤裤腰绳，在内监门处上吊自杀。13时12分，同监舍的被羁押人员发现乌上吊自杀后报警，看守所值班干警和医生立即赶往监舍现场处置施救，并迅速将其送往医院抢救。15时40分，医院宣布经抢救无效死亡。<br />
<br />
　　消息一出，引发外界热议。有网友怀疑乌小青“被自杀”，并列举了颇多疑点：上吊为何近一个小时未惊动监友？裤腰绳能否承受其体重？自杀位置真的正好避开了监控录像吗？乌小青明知自己罪不致死，为何在被调查了半年多后才自行了断？<br />
<br />
　　乌小顺也对记者表达了乌家人几点主要的质疑：<br />
<br />
　　1、按看守所的说法，同监舍的4人在睡觉，直到13点12分醒来才发现，而其间乌小青自杀，又避开了视频监控，那么，从何得知他于12点31分自杀的呢？难道从录像空白时间算起？<br />
<br />
　　2、乌小青如果想刻意逃避监控，晚上肯定比白天更不易被人发觉，为什么要选择12点31分这个午餐时间段？<br />
<br />
　　3、同监舍的四人，为何偏偏在这个非午休时间段同时睡着？人在非正常死亡之前通常会因为难受有所挣扎，发出声响，为何没人听见？<br />
<br />
　　一位昔日同事也颇感乌小青自杀时机不对。他说，一般刚被抓时，由于压力太大、紧张心理以及出于保护家人、掩护同伙等原因，容易发生自杀。“乌小青已经过了这关，该交代的已经交代了，赃款也吐出来了，现在即将进入审判阶段，他也应该知道自己不可能被判死刑。我们都在纳闷他是怎么想的。”<br />
<br />
　　据记者了解，重庆市第二看守所为近年来重庆新建，曾被评为国家一级看守所。它的管理和信息化监控，在全市监所中最为先进。职务犯罪嫌疑人多关押于此处。<br />
<br />
　　“一个有信息化监控系统的看守所，看不住一个重点疑犯？”一位参观过这个看守所的司法界人士说。<br />
<br />
　　重庆市第二看守所的纪检部门和重庆市检方已经介入了对事故的调查。有消息称，两名有关的监管人员已经因涉嫌渎职被刑事拘留，但没有得到官方的证实。<br />
<br />
　　11月29日下午，受重庆市高院委派，重庆市法官进修学院一行人前往第二看守所，协助各方做好乌小青家属的安抚工作，以防他们出现过激行为。<br />
<br />
　　前去的一位人士告诉记者，监控视频录像已被检察院封存无法看到，但他看到了乌小青留下的遗书。他以私人信函为由拒绝透露遗书的具体内容。<br />
<br />
　　乌小青之子乌江手里有遗书的复印件。这封遗书究竟写了什么？是否透露了乌小青自杀之谜？12月2日记者在乌小青家面见乌江时，乌江称暂时不愿提供遗书。<br />
<br />
　　12月1日中午，记者第三次拨通乌家电话，乌小青遗孀晏世平表示，他们不想接受任何采访，一切等检察院的调查结论。“我们一家有老有小，想要平静的生活。”电话那边说。<br />
<br />
　　记者还拨打了乌小青生前的司机冯伟的电话。冯伟一听“乌小青”三个字，在电话那头尖叫起来：“我什么都不知道，我也不在重庆，我在外地出差……我什么都已经忘了，你别问我了。”<br />
<br />
　　重庆“打黑除恶”<br />
<br />
　　收到万封举报信<br />
<br />
　　据重庆日报报道 1-2日，中共重庆市委召开三届六次全会，学习贯彻十七届四中全会精神，审议通过了《中共重庆市委关于贯彻〈中共中央关于加强和改进新形势下党的建设若干重大问题的决定〉的意见》。重庆市委书记薄熙来作工作报告。<br />
<br />
　　薄熙来指出，腐败大案要案让人警醒，而“小贪小腐”面宽人多，也不容忽视。他强调，腐败往往有个量变到质变，渐进到突变的过程。无论对“大贪”还是“小腐”，我们都要坚决查处，绝不容情！<br />
<br />
　　薄熙来说，如果说贪污腐败是党的“致命伤”，作风蜕化则是“慢性病”，也会影响党的生命力。有些人虽然没有违法，但作风蜕化，如果不注意，照样会“病入膏肓”，最终致命。<br />
<br />
　　薄熙来说，革命的道理千条万条，归根结底就是一条――全心全意为人民服务。作为共产党员，你文化再少再低不要紧，只要成天想着为人民服务，实实在在去为人民办事，就算基本合格的共产党员；反之，你文化再多再高，背离了这个宗旨，也不是真正的共产党员。这次“打黑除恶”，群众寄来1万多封举报信，80%是实名举报，也是一场人民战争！战争胜负、政权兴衰，最根本的是人心向背！<br />
<br />
　　薄熙来说，人生真正的财富是什么？是钱财吗？它害了多少人！积极做事，为国家、为百姓做事，才最高尚，也最充实！领导干部要算好“人生大账”，在有限的人生，“多做好事，少做错事，不做坏事”，这样心里才踏实。“一要干活，二要干净”，有了正确的人生观，才能活得充实、积极，才能干出很多有意义的事。<br />
<br />
　　薄熙来说，文化瑰宝是真正的财富，取之不尽，用之不竭，要撷取精华，为我所用。不要像那些腐败分子，不识文化瑰宝，只认金银财宝，要学到真东西，提高自己的知识层次和文化品位。<br />
<br />
　　重庆拟开展<br />
<br />
　　司法机关重要领导<br />
<br />
　　财产申报试点<br />
<br />
　　据新华社电 记者从日前召开的重庆市委三届六次全委会获悉，重庆拟开展司法机关部分重要岗位领导干部财产申报制度试点。<br />
<br />
　　今年1月至9月，重庆市共受理群众信访举报1．9万件，查处了厅级干部19人。一些被查处的干部曾在司法机关担任重要职务，其中包括重庆市司法局原局长文强、重庆市高级人民法院原副院长张|、重庆市高级人民法院执行庭原庭长乌小青、重庆市公安局原副局长彭长键等。<br />
<br />
　　重庆市检察院检察长余敏表示，目前检察院的反贪部门、执行批捕的部门等相关人员都已实行向组织申报工资、继承所得、投资收入等财产收入，确实起到了明显作用。<br />
<br />
　　王鸿举<br />
<br />
　　因年龄原因卸任<br />
<br />
　　黄奇帆<br />
<br />
　　任重庆市代市长<br />
<br />
　　据新华社电 重庆市第三届人大常委会第十四次会议3日通过决定，接受王鸿举辞去重庆市市长职务的请求，黄奇帆代理重庆市市长职务。<br />
<br />
　　今年64岁的王鸿举，重庆渝中人，2003年起任中共重庆市委副书记、市长，为重庆直辖后第三任市长。王鸿举在报告中称，因为年龄原因，向重庆市人大常委会提请辞去重庆市市长职务。</div>

							
							
							
							
