戴晴在讲话时开门见山地说：“我很高兴今天有机会在这里表达原本我想在我的故乡北京表达的话。开幕式上习近平说代表13亿中国人参加书展，我并不觉得我和我的一些作家朋友被他代表了，因为我们的书被禁止，我们出国受到限制。而习近平在讲话时只字不提中国的新闻管制，只字不提中国的书号还是由国家发放，只字不提被大家要求了多年的迟迟没有出台的新闻法和出版法，也只字不提赫尔塔&#8226;穆勒获诺贝尔文学奖。穆勒在作品中反专制。我倒觉得，穆勒代表我们的地方更多一些。” <br />
