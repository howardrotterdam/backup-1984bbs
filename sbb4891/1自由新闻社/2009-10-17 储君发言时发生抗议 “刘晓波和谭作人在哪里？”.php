笔者于周二跟北京的戴晴女士通电话，她让笔者跟德方项目负责人说明，她至今未收到正式邀请，因为德方邀请书（故意？）发到新闻出版总署去了，没有发给她个人，所以她无法去申请入德签证。笔者跟德方负责人多次联系后，明白了德方受到压力，其中有好几道坎儿，因而他们略施小计。好在德国笔会立即跟驻京的使馆沟通，戴晴女士很快得到签证并且直赴机场。一错再错的德方负责人竟然已经让汉莎航空公司将她的机票取消了，戴女士立即当场购买了新的机票，迳直飞抵法兰克福。于此同时，贝岭也从波士顿抵达。这些波折和幕后乾坤都被国际媒体追踪报道，德国书展的最高负责人博思先生被逼上梁山，只能在12日的研讨会上，将两位备受瞩目的“异议作家”安排在前面发言。<br />
<br />
由于中国官方施压，干预德方东道主的事弄得满城风雨，这两天的会议热闹空前，而扮演主角的中国官方代表团也没有辜负众人的期望，作出了极为精彩的“退场”、“返场”的表演。首先在开幕词中，法兰克福市长基民党的罗特女士申明书展的精神在于言论自由，东道主邀请了客人，就不得没有担当，收到压力就收回请柬。她还赞扬戴晴和贝岭两位排除困难来参加会议。中国官方的代表脸色铁青，如坐针毯。使馆参赞赵彬和前大使梅兆荣在发言时都进行反击，认为德方对宾主国的中国代表不尊重。<br />
<br />
当戴晴上台发言时，中国代表团的多数成员都站起来，退出会场。一时场面混乱，有点失控。戴晴和贝岭的发言其实都很温和，他们对“祖国”同行们的表现感到遗憾。于是在两位的发言完毕，以及德方的劝说和道歉（市长开幕词中的“倾向性”）之后，中国代表团又重返会场。梅兆荣这位外交官似乎并不在意自己的外交形象，他不仅对戴晴的提问拒而不答，并且指出戴女士不能代表中国人民。不知道他说这样毫无意义的话的用意何在。异议分子在中国本来就因为没有生存的空间而少之又少，戴晴当然不能代表中国的沉默大众，她代表的是一种追求言论和写作自由的勇气。<br />
<br />
代表团的一位社科院的“学者”的发言，为自己的身份做了最好的注解。他说中国共产党是代表中国大多数人的利益的。听众对这位仁兄的学识没有受到多大的“教诲”，但是对于他的政治观点和社会地位却十分领教了。<br />
<br />
这一场题目为“中国与世界――感受与现实”的讨论会，本来是中国学者们表现一个拥有古老文化的国家如何与现代文明接轨融合的良机，让西方社会了解中国人在两百年来的现代化的阵痛里的感受，他们的痛苦、失败、成功和骄傲。然而这么一批中共的官员和官方学者作家，就像大象踏进瓷器店一样，踩碎了人们美好的期待和希望。法兰克福书展的宗旨是弘扬思想、文化、出版、言论的自由，经过几年的磋商，终于下了决心邀请中国为宾主国，其用心良苦，人们以为这样做能促进提升中国的写作和出版自由，就像去年世运会在北京举行，西方以为能改善中国的人权，这不是缘木求鱼么？<br />
<br />
书展的正式时间是10月14日至18日，届时德国笔会将和独立中文笔会每天举办一场有关文学和政治的对话，劳改基金会也将设有摊位，台湾、香港都有展馆，西藏人和维吾尔人也将展出他们的图书。不知道那时候宾主国的中国还会拿出怎样的手段来面对这些“异类”，我们且拭目以待。<br />
