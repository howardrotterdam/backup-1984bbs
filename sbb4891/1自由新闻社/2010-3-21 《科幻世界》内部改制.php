自科幻世界员工为了科幻世界杂志的发展、集体提出意见的事件发生以来，各方朋友、各方媒体，尤其是广大的读者，给予了我们无限热诚乃至无私的支持，对我们杂志社局势的下一步发展表示了最大程度的关心。所以在这里，我们有责任把今天杂志社员工的进一步行为，给大家一个通报。 <br />
<br />
今天上午（3月22日），杂志社全体员工召开大会，并在会上统一了精神，决定按照正式的程序，向科幻世界的上级主管单位做书面汇报，请求上级领导予以重视，伸出援手（同时这也是其职责所在），关注并帮助解决我们科幻世界杂志所存在的实际问题，给科幻世界指明一条出路。 <br />
<br />
今天下午三点，上级领导召集杂志社全体中层干部参加会议，对于杂志社员工要求推举职工代表旁听的要求，则反复予以拒绝。在这个会上，某主管领导对杂志社的中层干部进行了分别谈话，并予以分别“提点”，对杂志社员工所普遍反映的问题，该领导当场所下的结论是“这不足以说明什么”，对科幻世界所面临的生死存亡的问题，他并不在意――他所关心和关注的重点，仅仅在于经济问题，而对杂志社的发展，却几乎可以说是置若罔闻。 <br />
