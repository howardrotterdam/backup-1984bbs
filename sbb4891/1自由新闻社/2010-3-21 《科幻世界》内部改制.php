号称世界发行量最大，经过杨肖、阿来名家经营的科幻世界最近惨遭斩首，为科幻世界兢兢业业十余年的美女社长秦丽（前不久还同外国科幻作家一同看看望灾区的同学们）被迫离职。昨天下午已经完成交接。新来的社长姓李，是原来科技报的社长，科幻世界当年接下办不下去的科技报，李社长于是离开科协。坊间传闻，李是现在科协某领导的兄弟伙，这次是专门调回来掌管科幻世界。 <br />
