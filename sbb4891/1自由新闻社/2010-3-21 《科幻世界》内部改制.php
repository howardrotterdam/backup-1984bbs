													<h2>《科幻世界》内部改制</h2>
						
						
																											<div id="postmessage_525438" class="t_msgfont">亲爱的读者朋友们： <br />
这是一封迟到的公开信。 <br />
我们曾努力想把一切惶惑和艰难压下心头，竭尽全力为我们挚爱的科幻读者奉上最精美上乘的精神食粮，但是今天，我们深切地意识到，如果继续容忍杂志社一把手李昶同志不懂装懂瞎指挥、不作为乃至胡作为，刚过而立之年的《科幻世界》很快就将面目全非。那不仅是读者的悲哀，更是中国科幻的悲哀。 <br />
众所周知，在过去的三十年里，《科幻世界》作为中国幻想期刊方阵的排头兵，为推动中国科幻事业发展、提升中国科幻创作实力、促进中西方幻想文化交流立下了汗马功劳。杨潇、谭楷、阿来、秦莉，几代领导人为了扩大《科幻世界》的影响力，为了让《科幻世界》始终保持一个又一个“第一”， 无私奉献，殚精竭虑，在中国期刊界创造了一个又一个奇迹；因为他们的前瞻眼光和无畏勇气，1991、1997、1999、2007……，这些平凡的数字成为了幻迷心中不可逾越的丰碑。《科幻世界》因他们而蓬勃，因他们而充满无限活力！ <br />
遗憾的是，随着李昶同志走马上任，《科幻世界》这本原本极具雄心和视野的杂志，很快变成了井底之蛙、鼠目寸光。《科幻世界》既没有近期目标，更无法奢谈长远规划！作为一群对《科幻世界》和读者极端负责任的员工，我们认为，在这个极其关键的危机时刻，只有向读者们说出真相，我们才能从道义上得到全社会最广大的支持，我们才有继续坚持下去的勇气和动力！因为在我们心里，每一个读者都是我们最可信赖的朋友，每一份支持都是我们最期盼得到的回音！ <br />
<br />
现将李昶同志的部分所作所为给大家汇报如下： <br />
1、李昶同志原是一家地方小报的副总编辑，对现代期刊出版行业缺乏基本了解，却又自以为是，妄自尊大，一味瞎指挥。在2009年上台后不久，他就异想天开地提出所谓新主张：中文编辑取代作者写小说，外文编辑取代译者译小说，美术编辑取代画家画插图，完全不懂作者与编辑的社会分工，企图将《科幻世界》办成一个农业时代的小作坊。这种提法一旦被强行推动，必将把科幻世界花费三十年才建立起来的与作者、译者、画家之间的紧密联系破坏贻尽，彻底葬送科幻世界！为了维护读者和作者的利益，杂志社全体编辑和发行人员都强烈发对，最后终于躲过一劫！ <br />
2、李昶企图将科幻世界的封面变成学校的广告图片。面对他的错误指示，各部门强烈反对，但李昶同志却在公开威胁有不同意见的员工：“不是不报，时候未到！”这种无视办刊特色、无视读者需求和员工意见的蛮横态度，极大地伤害了员工的感情。有非常优秀的骨干员工抵挡不住巨大的心理压力，哭着离开了为之奋斗多年的杂志社；有作者得知此讯，立即提出撤稿。为避免更多的员工离开，确保杂志发展的大方向，编辑部和发行部只能尽量拖延，但矛盾随时可能激化。 <br />
3、不顾读者利益，一味强调节约成本，将《科幻世界》的用纸换成了劣质纸张；同时强行要求各刊缩减稿费标准，甚至要求封面使用200元一张的低劣作品；严重减缩应该支付给版权代理商和作者的费用，一再拒付作者或拖延稿费，使我们无比珍视的作者和读者的权益受到前所未有的伤害！ <br />
4、将科幻世界杂志社广告资源出让给私人朋友的广告公司，暗中支持广告公司挤占刊物版面，将杂志社的采编、栏目设置、发行渠道等一系列权益拱手出让。导致的直接后果是，广告公司为了获取最大利益，自2010年第1期开始，大量广告强行挤占杂志社各刊版面，在读者中造成了极其恶劣的影响和不可估量的损失，杂志社沦为了个人敛财的工具！ <br />
5、寻找各种理由拒绝或者延缓和编辑签订劳动合同，连一年一次的体检他都认为是在员工身上白花钱！经常嘲笑编辑们的开创精神和主人翁意识，在他眼里，编辑成了毫无尊严的，极其低廉的劳动力，认为800元在哪里都能招聘到人，常年用极低的工资待遇逼迫杂志社的编辑一个个离开自己热爱的科幻事业，在多名员工因不满李昶的高压离职后，他还变本加厉，明确要求各部门不得补充离职人员留下的空缺，致使各刊的上市时间严重滞后，在市场上造成了极端恶劣的影响。他还多次企图将自己的关系人员（这些人对科幻毫无了解，连基本的出版业务知识都不懂）安排到科幻世界来做编辑。 <br />
6、大搞一言堂，严重挫伤员工的积极性，自他上任以来，一手遮天，将杂志社以前多项优秀的管理制度和企业文化破坏殆尽。面对员工的不满与愤怒，李昶同志多次在各种场合和会议上炫耀他的上层关系网，狐假虎威，警告想要上告的员工不要以卵击石，营造自己不可撼动的声势！ <br />
7、李昶同志上任不久，就将科幻世界旗下的杂志“一号多刊”地公开叫卖，出租给那些毫无出版资格的公司和个人，杂志的编辑出版完全失控，严重冲击杂志社的正规出版物，极度毁损杂志社品牌形象。严重违反《国家期刊管理条例》的多条明文规定，杂志社将面临停业整顿和被吊销刊号的巨大危险！ <br />
<br />
但就是这样一个不懂科幻、不了解出版市场与编辑业务、不尊重读者和作者、思想品德败坏、无德无能的人竟然一路爬升，挤走科幻世界原社长，成了国家级品牌期刊刊社的社长和总编！现在科幻世界人心惶惶，随时可能出现全体编辑集体辞职，中国科幻最后的一面旗帜将在不复存在！ <br />
在这艰难的时刻，为了维护科幻世界的荣誉和广大读者的权益，面对巨大的压力，编辑们、发行人员站了出来！普通员工们站了出来！我们已经向有关领导和部门寄送了详细汇报材料，要求撤销李昶同志在杂志社的一切职务，重新公开选举一位业务素质高且能够带领科幻世界走出迷茫的新领导！虽然我们深知要处理这样无作为、乱作为，靠拉关系走后门爬上来的腐败无能干部，可能遭受意想不到的阻力，但是我们坚信光明与正义的力量，为了我们热爱的科幻世界，为了我们可敬的读者，为了背后坚定支持我们的作者，我们为此将纷争到底！ <br />
我们深知，这样抗争在短时间内可能会给广大读者造成诸多不便，为此我们深表歉意。我们真切地希望广大作者、读者朋友能理解我们，支持我们。因为，我们的科幻，已经到了不得不背水一战的时刻！ <br />
