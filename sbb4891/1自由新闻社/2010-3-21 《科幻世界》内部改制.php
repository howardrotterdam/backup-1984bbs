【我只陈述事实：从09到10年，《科幻世界》发生了哪些变化&nbsp; &nbsp; &nbsp; &nbsp; 】<br />
很遗憾很抱歉我不是忠实读者，从09到10年，我总共只买了三本《科幻世界》正刊，还有一本《译文版》。 <br />
看到了最近发生的这件事情，我赶紧找出了以前的科幻世界一一对比，发现了不少有意思的东西。 <br />
这些是内部人员可能有所忌讳，可外人能够畅所欲言的东西。 <br />
提倡大家人肉出所有的G点！ <br />
我买的包括2009.1，2009.3，2010.4（不好意思，这是我几小时前特意买的停刊特别纪念版）。 <br />
首先，我对比了人员构成。 <br />
至少从09.1开始，社长兼总编就是“李昶（这个字读chang厂）”。 <br />
从09.1到09.3，人员变化不大，文学编辑中，减少了“白俊霞”，增加了“姚雪”。其余没有任何变化。<br />
从09.3到10.3，这hll的改革呀： <br />
文学编辑中，减少了“李克勤”“明先林”“陈颖”，这三个原来都是文学编辑中排名前四的，仅次于“刘维佳”。新增了一个“刘壮”。 <br />
同时，“文学编辑”不再叫“文学编辑”，改名为“编辑”。 <br />
原来所有的美术编辑都消失了――“美术编辑取代画家画插图”――包括“黄远霞”“王莹莹”“徐杰”，取而代之的是一个新的美术编辑“黄希”。 <br />
同时，新增加了“网络部”，网络部主任为“明先林”――原来他从文学编辑转为网络部主任了啊。 <br />
网络部编辑为“王兴”“果露怡”。 <br />
原来的《科幻世界杂志社》只有一个广告许可证号，没有广告代理。 <br />
在2010.4的正刊上，我看到了一个叫做“四川兴聚仁文化广告传播有限公司”的“广告总代理”，同时，新增加了“广告部运营总监”――“王晓波”，他有一个非常山寨的邮箱： <br />
<a href="mailto:xinsheng_1@163.com">xinsheng_1@163.com</a>. <br />
另一个变化是，“编辑部投稿信箱”从四个变成了一个，“校园之星”“其他栏目”“星云中长篇”对应的投稿信箱都被整合到了<a href="mailto:tougao@sfw-cd.com">tougao@sfw-cd.com</a>。这是人力资源的节约，还是管理水平的倒退？ <br />
最后需要注意的一点是，《科幻世界》的主管主办单位是“四川省科学技术协会”。 <br />
引自 <br />
<a href="http://www.sckx.org.cn/ThkCOSNews/2006-10-09/632960088722530000.htm" target="_blank">http://www.sckx.org.cn/ThkCOSNew ... 960088722530000.htm</a> <br />
在这里，我们可以发现，“《科幻世界》杂志社”和“四川省反邪教协会”是同一个层面上的事业单位，所以――“没有《科幻世界》又怎样？” <br />
