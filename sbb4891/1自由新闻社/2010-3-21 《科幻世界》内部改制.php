该领导还称，召开会议本身已说明科协“关心《科幻世界》”，因为“没有《科幻世界》又怎样？” <br />
<br />
现在，已经晚上八点了，我们很多员工仍然坚守在科幻世界的办公大厅里，商量着明天的道路，商量我们应该继续怎么做。我们大家，当初都是怀着一股热血和激情，来到这里，希望能为中国的科幻、中国的幻想事业、也为了自己的梦想而奋斗。 <br />
<br />
科幻世界，她不只是我们现在几十位员工的杂志，一代又一代，她凝聚了无数员工、更凝聚了无数幻迷的光荣与梦想，它是属于我们大家心灵里的一片净土。 <br />
<br />
科幻世界是一个小小的杂志社，但在某种意义上，她又是一个很大很大的家园，也许她在过去的若干年里，有过这样那样的坎坷，让大家有了这样那样的遗憾，但毫无疑问的，她寄托了许多人的梦，而且她能够托起更多人的梦。 <br />
为了这个，我们还将继续斗争下去！我们也在此呼吁所有关心科幻世界、关心中国的幻想事业的朋友们，给予我们最宝贵的支持！ <br />
