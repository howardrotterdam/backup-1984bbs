&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;周凤臣居士肉身记<br />
 2010-07-16 16:38 | (分类:默认分类)<br />
&nbsp; &nbsp;&nbsp; &nbsp; 吃素念佛，享年八十八岁，已往生两年多的周凤臣老居士肉身不腐的事，已传遍了整个北京城。《新民晚报》《人民公案报》《人民日报》海外版等传媒均对此作了报导。一九九四年十月三日，我们中国佛学院一行十四名学僧，专程去河北省香河县瞻仰了周老居士的肉身。<br />
<br />
 <br />
<br />
　　周老居士的长孙，北京某部的一位少校军官--杨学强先生，热情地接待了我们，领著我们观瞻了老居士的遗容。<br />
<br />
 <br />
<br />
　　老居士的肉身安置在她旧时居住的房间里。这房子是单独的一个套间，是老居士生前叮嘱儿孙千万别拆而留下的，其馀的都被拆除。里间不到十平方米，只放著一张供桌和一张床。供桌上方的墙壁上挂著老居士的遗像，老居士就躺在床上，身下垫著一条毛毯，身上盖著一块黄布。杨先生对我们的前往非常高兴，给我们以特殊照顾，一般的人前去，他说只是随便的给他们看看。而对我们，他不但把盖在老人身上的布拉开，而且还捋起老人的袖子和裤脚管给我们看。老人的肌肉已呈琉璃，透明状态。一看之下，我们不由得产生十分恭敬的心情。他还指著老人的头部告诉我们，老人头上额角两个地方，在往生不久脱水时不慎被擦破了皮，细菌也未能感染，到後来自动愈合了。确实，头上有两处硬币般大小痕迹，还露出新鲜的皮肤，真是不可思议！人停止呼吸两年多肉身不腐烂，这本就很不可思议，况且把皮肤损坏了细菌还不能感染，这就更难以理解；又没有采取什麽措施来防腐，里外间是相通的，可供人进去瞻仰，老人的第二个孙子就住在外间，还每天进去给老人烧香和看护老人。对於周老居士的肉身，人们都感到不可思议，而当人们知道了周老居士形成肉身的过程後，却不知会作何种感想了？<br />
<br />
 <br />
<br />
　　老居士七六年随长孙来到北京居住，直到一九九二年。九二年三月份，老人提出要回家，从三月份拖到七月，长孙一家没让她走，到了八月份，她说一天也不能再待了，於是杨先生才送她回香河老家。从八月到十一月，老人身体都还很健康，到了十一月六日，身体就发生了转变，突然得病，病得也很怪，医生诊断是肺炎，体温却一点都不高，只是咳嗽，其实应该说是呕吐；不进食，只喝点水，喝一口水进去反而要吐一碗多的东西出来，是一些方的圆的长的短的，红的黑的黄的褐的等各种形状各种颜色的痰块物。一连五天，把胃里所有的东西都吐了出来。不但每天呕吐，而且每天也排大便，排大便也和常人完全不同，她不是排而是喷，一说要排便就喷然而出，量也特别大，而且也是各种颜色的都有。排到最后，连一种油质的东西也排了出来，真不知这麽多的东西是从哪儿来的。老人本来是在医院住院的，但到了十一月十号上午，她就再也不愿在医院待了，要求回家，家里人不让她回家，她就说，你们不让我回家，我让你们都後悔，说完就不动了。家里看护她的人一看她声息全无，一摸之下，心脏和呼吸都停止了，不由大惊失色，赶紧说，那就回去吧。老人奇迹般地又活了过来，高兴地说：「行，咱们回家吧！」家里人都非常惊奇，老人家怎麽能控制自己的心跳和呼吸？当时老人的女儿亦在场。<br />
<br />
 <br />
<br />
　　十一月十日晚上杨学强先生也从北京回到老人身边，他是老人的长孙，从小老人特别疼爱他。家中的人以为老人怕是不行了，於是按照传统风俗习惯，给她穿好了寿衣，准备办理後事了。岂知老人回到家里後反而出人意料地康复了许多。<br />
<br />
 <br />
<br />
　　十一月二十日老人要求回北京，孙儿又把她接回了北京，一九九二年十一月二十四日晚上十点半许，老居士就在北京长孙家里安然往生了。老居士在往生前给儿孙们留下了五条遗训：一、走到天边说话都要口对著心；二、遇事要多为别人著想，不要光想着自己；三、受人滴水之恩要涌泉以报；四、钱财是身外之物，生不能带来死不能带去，对化缘修庙的和讨饭的人，要给钱；五、做一件好事会有人知道，做一件坏事也会有人知道，要多做一件好事，少做一件坏事，告诉他们做人的道理之後，就说她要睡觉了。<br />
<br />
 <br />
<br />
　　老人往生後二十四小时体温不退，还保持著正常的温度，多日後肢体仍柔软如常，手背及指甲由白变红有血液流动，肌肤红润如生。全家人本就对老人十分依恋，真只当奶奶睡著了，在感觉中一点也没把她看成已与世长辞。因此，老人仙逝多日都没有对她的身体怎麽处理，看到这异常情况後，就更没有把她火化或运回去土葬，只盼她能在某时醒转过来。现在儿孙都出息了，家庭条件早已改善，正是老人家享福的时候，孝顺的儿孙谁不希望自己的爷爷奶奶多活些时候？所以老人的遗体一直停放在长孙家老人的卧室里。<br />
<br />
 <br />
<br />
　　杨先生对我们说，在老人停止呼吸的时候，他们根本还不知道什麽叫肉身。直到老人往生的第十五天，他上班後到一个同事家去聊天，看到桌上有一大摞报纸，顺手拿起一张，就看到一则报导九华山大兴和尚肉身的报导，你说奇不奇？他一看这报导所载和他奶奶的情况很相似，就赶快拿回家里给家人看，於是才知道老人可能变成肉身了。他们就想把老人的身体保存下来。杨先生就去广化寺找怡学法师。跟他说起这事。学愚法师对他说，老居士留下这肉身，就是给世人留下一个生动活泼的修行榜样，要好好保护，佛教团体的力量太小，应该借助科学部门的力量。後来他又去找中国人体科学研究院，人科院派了一位一级教授带队前往，他是学密宗的，他看了老人遗体後，特别激动地说，老人家是靠自己的修持，达到了一个很高的层次。在二十世纪的末期我们中国能出现这样一位老人，是我们中华民族的骄傲。之後，人科院就下达了保护老人肉身的通知。杨先生在当天早晨做了个梦，梦中奶奶对他说，从今天开始，你们办我的事就不用再偷偷摸摸了。人科院就是在那天中午派人去的。<br />
<br />
 <br />
<br />
　　对於老人肉身存留的整个过程，完全是周老居士自己一手安排的。回到北京，这是首要的一个条件，如果在农村往生的话，不要说不知道她的身体能保留於世，即使知道，谁又敢把她留下？按照农村的风俗，两三天後就得埋葬。其次，她没有告诉孙儿一家她要在什麽时候离开这个世界，於是孙儿一家在老奶奶往生後，没有慌恐，没有惧怕，只是在恍惚中期待老人回来。老人回北京时还告诉家里人不要让乡亲们知道她回了北京。老人这样做是有深意的，她知道，如果说出某日某时她要往生，儿孙们一定会很悲痛，这样不但会使她的往生西方受到阻碍，而且也会打乱她的计划，使身体火化掉。即使不作处理，但乡亲知道了她仙逝，也绝不会同意把她的遗体存放这麽久，这都是老人预料之中的事。肉身几经周转，从北京运回到河北老家，经历重重困难，冲破层层阻力，特别是社会舆论，到如今总算能得到一些社会有识之士的认可，以及社会的善意的宽容了。<br />
<br />
 <br />
<br />
　　周老居士的「预知时至」虽然没有亲口说出，但从她所做的具体安排就完全可以看出，老居士是预知时至的，因为老居士是有修行的行者。她所达到的境界，非吾辈凡夫所能知。<br />
<br />
 <br />
<br />
　　老居士心地善良，从小就发愿长大後要给人治病，也不知她是否学过医，不过後来她的确真的能给人治病了，而且往往手到病除，曾治好许多连医院也治不好的疑难杂症，对於有病的人，别人来求她，老人给他们治病，别人不来求她的，她知道後也会到人家里去给人医治，而且她给谁治好了病，从来都不跟家里人讲，这都是乡亲们後来自己谈起，要不家中人还不知道。<br />
<br />
 <br />
<br />
　　在文革期间，老居士也受到了攻击，抄她的家，不准她再给人治病。家中值钱的东西都给抄走了，但老居士却把一瓶给小孩治病的白蜂蜜设法保存了。虽然不让她再给人治病，但她还是坚持给人医治，白天不准看就叫乡亲们晚上来。她说哪怕让她第二天就活不成，别人有病，她也得给人治。老居士给人治病，从不图报酬，别人给她，她也不收，最多只取一些水果之类的东西拿来供佛。<br />
<br />
 <br />
<br />
　　她的娘家也在香河县。父亲是清朝的一位官员。老居士出嫁时娘家的陪嫁物中给了她一个佛龛。这个佛龛我们都看见了，古色古香的，现在还供著西方三圣的圣像。在周老居士所住的落後农村，没有寺院，原有的寺院在文革中被毁坏了，那些村民们没去过寺院，没见过什麽出家人，他们对佛教没有亲切感，对出家人没有恭敬心。从我们这一次的行程中已真实地体会到了，他们根本谈不上对佛教的信仰。然而盛唐时「家家弥陀佛，户户观世音」的佛教传统，至今仍具影响力。周老居士从三十八岁起就断荤茹素，直到终年八十八岁，五十年始终如一。她反对杀生，特别是计划生育的打胎，要是成了人形而把他打下来，等於杀了一条人命，罪孽深重。<br />
<br />
 <br />
<br />
　　在周老居士身上有著许多不可思议的神通感应，比如说她到北京住以後，农村家里发生了什麽事她都知道，而且有时家里人或乡亲们中的那一个人要到北京来看她，她早晨就知道了，说要留饭，今天中午有谁来，一定来，这一天就真有人来，分毫不差。周老居士虽然八十多岁了，但思路一直非常清晰，在要往生之前的时候，曾安排搞过一次卫生，各人分工不同但都合适，吩咐她的女儿擦地板，这不是用拖把拖，而是手拿抹布蹲在地上擦。她的女儿在以前腿上有老病，但自擦过地板後就不治而愈了；老人往生前，有二十多人看到一二里远的西方上空有几个红红的火球在燃烧著，（包括家人和外边人）有人看到的是三个，有人看到的是四个，大小也各自看到的不同，但都同时看到了。对於老人的许多奇怪的事情，老人的小孙子要把它记录下来，老居士在另一个房间都知道，告诉他不要写，以後有人会写的，老人的孙子没有听，但写著圆珠笔的笔芯却突然不翼而飞，大家无不惊奇。<br />
<br />
 <br />
<br />
　　在老居士往生後一个月左右的时候，老人的女儿和杨先生的两位同事无意中用手触摸了老人的身体，手上就沾上了一股香味。有的是左手触到，有的是右手触到，左手触到的左手香，右手触到的右手香，另外一只手却不香。这种香不是一般化妆品的香，而是一种说不出的幽幽清香。香是一直延续了一个月，洗都洗不掉；周老居士的肉身已经历了两个多春夏秋冬的严寒酷暑的考验，一点也没有腐化的现象。这许许多多的怪异之事，对社会中一般人来说是确实不可思议，难以相信的，他们可能不会认为这事是真的，即使有些人相信，也会觉得这只不过是十几亿人当中的偶然罢了，末法时代的众生当真难调难伏！就是我自己，也是如此，信疑参半，特别是对这肉身，也认为这可能是存放地方的地质或环境特异而已。<br />
<br />
 <br />
<br />
　　我虽然是个佛教徒，出家亦几年了，但仍有一种眼见为实的科学的固执态度，实事求是，从不轻易相信道听途说。自我亲自观瞻了老居士的法体，亲耳闻听了老人长孙杨先生的叙述後，才真正对老居士钦服了。因为在老居士身上并不存在偶然，她的遗体在往生後已移动了许多地方，亦不是封闭的，而是存在自然环境之中。这对於一般的人的遗体来说，自然是无法解释的，而对於茹素学佛的人来说就不难解释了。佛法能解释一切，无论精神的，物质的，乃至宇宙人生，都可以对之作出透彻的解释。这就是佛法的不可思议。<br />
<br />
 <br />
<br />
　　周老居士在这个世界上示现肉身，留下肉身，是为了在这众生颠倒妄执，不信因果，贪嗔痴丛生的末法时期，示现修行证果的事实，从而让大众都对佛法生起正确的信仰，都来学佛信佛。她老人家生前曾说，要让全北京的人，全国乃至全世界的人都知道她的名字，都来信奉佛法。在老居士往生後不到两年的时间，北京市的人绝大部分都知道了老人的事迹，一提起周凤臣老居士，几乎人人皆知。外地亦有不少人知道了，且有一部分不远万里亲到河北瞻礼。老居士的初步愿望已基本实现了，相信她的最终愿望也定会实现的。<br />
