													<h2>央视揭秘灰色产业链 称网络黑社会操控舆论</h2>
						
						
																											<div id="postmessage_383818" class="t_msgfont">from cnbeta ：<a href="http://www.cnbeta.com/articles/100411.htm" target="_blank">http://www.cnbeta.com/articles/100411.htm</a><br />
<br />
<br />
互联网是我们生活的一部分，但在网上看到的消息，新闻，特别是论坛里的很多帖子、话题，可能有很多都是不真实的，我们的记者就发现了一个潜伏于水下的灰色产业链。据中国互联网络信息中心统计，目前中国网民数量规模已达3.38亿人，普及率达22.6%，超过全球平均水平。互联网几乎成了我们生活的一部分。如 果我告诉你，你在网上看到的消息，新闻，特别是论坛里的很多帖子、话题，可能有很多都是不真实的，你还会对那些不时爆出来这样门那样门，或者是所谓的猛料 兴趣不减吗？我们的记者在对一个的热点事件的采访中，发现了一个潜伏于水下的灰色产业链，现在我们就来看看这到底是怎么一回事。<br />
<br />
<img src="http://img.cnbeta.com/newsimg/091219/23512901837361837.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
“潜伏水下的灰色产业链”浮出水面<br />
<br />
2009年7月份，奇虎360公司正式推出了一款免费杀毒软件，承诺永久免费杀毒服务，这个举措让360立刻成为了杀毒软件行业里的最抢眼的的角 色，但是，一个有关奇虎黑幕的质疑也接踵而至。7月29日，一篇名为“奇虎离职老员工的告白”的帖子频繁出现在各大论坛，作者通过自己离职经历，曝出“奇 虎暗中搜集用户的隐私数据”等种种问题，帖子一出，就迅速吸引了数十万的点击量和回帖，尽管公司回应这是竞争对手伪造，但是一时间这件事显得真假难辨，而 就在10月下旬， 这篇帖子再次成为论坛热帖。<br />
<br />
奇虎360公司公关总监屠建路:“光是刚才说的前后这两篇，这种模仿奇虎离职员工，这种口吻写的前一个离职信就是有数千条像这次也有上千条。”<br />
<br />
让360公司感到蹊跷的是，为什么像员工爆黑幕这样的帖子能够在一夜之间就覆盖了几百家论坛，而且总是能引发不可思议的回帖量。<br />
<br />
奇虎360公司公关总监屠建路:“从去年7月17号，我们推免费杀毒以来，这一年多的时间，我们在互联网上遭受这种，恶意的负面的贴子数量应该是在数十万以上。”<br />
<br />
