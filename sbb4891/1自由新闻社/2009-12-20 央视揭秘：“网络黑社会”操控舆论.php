													<h2>央视揭秘：“网络黑社会”操控舆论</h2>
						
						
																											<div id="postmessage_383806" class="t_msgfont">互联网是我们生活的一部分，但在网上看到的消息，新闻，特别是论坛里的很多帖子、话题，可能有很多都是不真实的，我们的记者就发现了一个潜伏于水下的灰色产业链。据中国互联网络信息中心统计，目前中国网民数量规模已达3.38亿人，普及率达22.6%，超过全球平均水平。互联网几乎成了我们生活的一部分。如 果我告诉你，你在网上看到的消息，新闻，特别是论坛里的很多帖子、话题，可能有很多都是不真实的，你还会对那些不时爆出来这样门那样门，或者是所谓的猛料 兴趣不减吗？我们的记者在对一个的热点事件的采访中，发现了一个潜伏于水下的灰色产业链，现在我们就来看看这到底是怎么一回事。<br />
<br />
“潜伏水下的灰色产业链”浮出水面<br />
<br />
2009年7月份，奇虎360公司正式推出了一款免费杀毒软件，承诺永久免费杀毒服务，这个举措让360立刻成为了杀毒软件行业里的最抢眼的的角 色，但是，一个有关奇虎黑幕的质疑也接踵而至。7月29日，一篇名为“奇虎离职老员工的告白”的帖子频繁出现在各大论坛，作者通过自己离职经历，曝出“奇 虎暗中搜集用户的隐私数据”等种种问题，帖子一出，就迅速吸引了数十万的点击量和回帖，尽管公司回应这是竞争对手伪造，但是一时间这件事显得真假难辨，而 就在10月下旬， 这篇帖子再次成为论坛热帖。<br />
<br />
奇虎360公司公关总监屠建路:“光是刚才说的前后这两篇，这种模仿奇虎离职员工，这种口吻写的前一个离职信就是有数千条像这次也有上千条。”<br />
<br />
让360公司感到蹊跷的是，为什么像员工爆黑幕这样的帖子能够在一夜之间就覆盖了几百家论坛，而且总是能引发不可思议的回帖量。<br />
<br />
奇虎360公司公关总监屠建路:“从去年7月17号，我们推免费杀毒以来，这一年多的时间，我们在互联网上遭受这种，恶意的负面的贴子数量应该是在数十万以上。”<br />
<br />
更让他们头疼的是， 在奇虎360的官方网站上，出现了越来越多地指责和叫骂，而公司技术人员发现，其中很多帖子呈现出一些值得琢磨的特点:比如说，这些帖子基本上都出自新注册的用户，发帖之后就再也不会使用，而且内容一样的攻击性回帖，会同时出现在好几个论坛。<br />
<br />
奇虎360公司工作人员:“短时间发很多贴，而发贴内容基本上一样，就是然后在后台，我们可以查询到，它发贴的ID或者发贴的IP，可以说是同一个IP或者说他们同一两个IP，打开比如说翻到第二页，第二页全是枪贴，有时候十几个贴子，可能一看是同一秒发的。”<br />
<br />
事实上， 这仅仅是众多网络攻击事件中的一个。从杀毒软件的口水战到“封杀王老吉”事件，从“康师傅”的“水源门事件” 到目前打得不亦乐乎的真开心和假开心，以及在网络视频领域的搜狐与优酷的盗版之争，网民们都能看到层出不穷的网上大战，而网友发帖，回帖，指责甚至谩骂攻击，更是让互联网这个虚拟的世界冒出了真实的火药味，然而，在这一桩桩口水战背后，是什么样的力量让他们愈演愈烈，越战越勇？<br />
<br />
奇虎360公司公关总监屠建路:“那普通这种网友是不可能做出来，就发一篇贴子，然后在短短的几周内，就迅速去扩展到，蔓延到几百上千，数千这样的 一个论坛里面，这个我觉得应该完全可以算得上，是一种网络黑社会，因为只有这样一种，专业这种组织在背后推动，才有可能会出现这种结果。”<br />
<br />
“网络黑社会”到底是怎么回事？<br />
<br />
网络黑社会，听着够新鲜的，以前还真地没听说过，但是对现实生活中的黑社会大家都不陌生，特别是今年的重庆打黑更是让大家看到了黑社会对整个社会经济、生产和正常生活秩序的破坏，黑社会是整个社会的毒瘤，必须要根除掉。<br />
<br />
那么真的有所谓的“网络黑社会”吗？“网络黑社会”到底是怎么回事？记者:“真的像这家公司所说的这样，有这样一个网络黑社会吗，我们在网上看到那 些各大论坛，博客上出现的纷争与攻击，真的有某种组织在人为操作吗，为了弄清楚这个问题，我把最近半年来，网络社区，也就是各种博客以及各种论坛中传播最 广的帖子通过搜索引擎搜索出来，并用电脑软件进行了整理，根据这些相关的主贴每天出现的数量制作了相应的波形图。”<br />
<br />
在记者选取的20个样本中，包括关注度极高的社会性事件，比如呼和浩特越狱，北大校长推荐制，也包括搜狐优酷盗版之争，康师傅水源门等商业性热点事 件 。那么这些波形图分别有什么样的特征，并且能说明什么问题？记者为此找到了中科院研究生院管理学院副院长吕本富。作为业界知名的网络经济专家，他告诉记 者，在这些波形图中，一部分是符合正常传播规律的。<br />
<br />
中科院研究生院管理学院副院长网络经济专家吕本富:“这个大学生甲流事件，也很正常，类似的新闻曝光以后大家就关注，然后逐渐逐渐就下来，就这个峰 显然是就其他媒体，我们叫平面媒体，或者电视媒体，对它有一个传播加强的作用，才会有第二个峰，然后下来就基本上属于正常曲线。”<br />
<br />
吕院长告诉记者，按照正常的传播规律，一个热点话题在网上的传播高峰时正常为三天，如果持续维持是违背自然传播规律的，在三到五天后，如果突然再次 出现传播的高峰，又没有特殊的原因导致旧话重提，这样的高峰就极可能是人为推动上去的，经过他对这些波形图的分析，一度被热炒的王老吉添加门，360离职 员工爆黑幕，以及康师傅水源门等事件，都明显出现了人为操作的痕迹。<br />
<br />
中科院研究生院管理学院副院长网络经济专家吕本富:“这个添加门事件也是这样的，那么高峰正常的话应该就下来了，那么结果又有第二个高度了，而且第 一个高度和第二个叫做中间差了好几周的时间，那么这也是有人为操作的痕迹，除非这个波峰的时候像央视的新闻，又给它曝光一次，才会有第二个波峰，否则的话 是不会有的，那么当其他的媒体新闻没有曝光它的时候，它又有这个波峰，显然这个峰也是人为操作的。”<br />
<br />
吕院长告诉记者，在普通人以为被偶然爆料，甚至一些被监督曝光而出现的热点话题，其实大部分是被一股潜藏的势力在操纵。近几年来，传统广告公关业务 正在迅速走向网络，网络营销的业务应运而生， 但是，其中一些以“公关公司” 或“营销公司”名义注册的公司，不仅能为企业提供品牌炒作、 口碑维护等服务，甚至也能按客户指令进行密集发帖，诋毁、诽谤竞争对手。而且，在最近三年来，这股势力群体越来越庞大，因为他们暗中操作，帮助客户打击异 己，攻击对手，因此又被称作网络打手公司。<br />
<br />
<br />
<br />
网络公关公司都必须雇佣大批的人员来为客户发帖回帖造势，这个工作常常需要成百上千个人共同完成，那些临时在网上征集来的发帖的人在行内被叫做“水军”。<br />
<br />
记者:“比如说对一些网络社区里的热点话题，你觉得有人专门在制造和操纵的，大约占到了多少比例？”<br />
<br />
中科院研究生院管理学院副院长网络经济专家吕本富:“我没有做过精确的统计，但是我想一半对一半是有可能的，就不能说所有的热贴都是被设计的，但至少有一半，50%左右热贴是经过人为设计的。”<br />
<br />
网络操控是否左右舆论方向？<br />
<br />
我们不仅从网上获取大量的信息，也会用这些信息来进行是非判断。如果真的像吕本富教授说的那样，互联联网的所谓新闻热点有一半是人为制造人为地这样 制造，那哪里还有真正的网络民意？为了进一步弄清这些网络打手公司的势力有多强大，他们到底是怎么操控一步步来按照自己的目的来引导甚至控制舆论方向？我 们的记者找到了一些所谓口碑营销的公司进行了深入地调查。<br />
<br />
记者在网上随机查询到一些网络公关公司后，以客户身份跟他们进行了接触。记者通过QQ告诉对方，目前自己所在的公司正在参与一个项目的竞标，一家在 业界知名的大客户是自己最有力的竞争对手，希望这家公关公司在竞标关键期间，炒作对手的负面新闻，让对方处于被动。 这位接待员的答复显得心领神会。而最终记者见到了这家公司的营销总监，他表示操作起来没有任何难度。<br />
<br />
某网络营销公司营销总监:“你要只有两三万的预算，搜集一下觉得哪个点是比较恶性的，在它这个基础上单独做一两个，挑一两个重点论坛把这件事炒起来，网上最近两周左右，它的负面信息增加到500条 600条，最后到一种什么样的程度，从百度谷歌搜录基本上前几页，全是它的负面信息，这也就是你这个费用在一两个月之内，能达到的最好的效果。”<br />
<br />
对于什么样的话题比较容易火，另一位营销经理表现得把握十足。<br />
<br />
某网络营销公司营销总监:“可信度非常高，从企业集团构成内部管理，要不就说它的资金链，这是影响一个企业比较致命的东西，有什么样的组织势力，让 人人心惶惶，要不就谁跟谁非法勾结，内部员工爆料黑幕或者说在饮食上做手脚，我们这个原材料经过多少道筛选，实际上都是一些地摊货。”<br />
<br />
当记者表示，如果有进一步的投入， 这个负面事件到底能传播到多大程度，这位营销总监更是一副轻松的口气。”<br />
<br />
某网络营销公司营销总监:“本身我自己的版主数就不下百个，所有的门户各个版主的联系方式，基本上我都有，本身每一家可能有各自控制的资源，比如说我自己的员工当版主，现在基本上每个员工手底下都有四五个版主号，在维护着四五个这样的版块，作为我们经常合作操作的。”<br />
<br />
在记者随后走访的多家网络营销公司，都能听到这样的宣传。<br />
<br />
某网络营销公司营销总监:“我们手下有20个团队，每一个团队都是一个群，每个群里面至少有50人，每一个人在全国的各大论坛都有很多ID号，这是我们能控制的，我们为什么能让很多企业，在一夜之间让他正面消息在网络上满天飞。”<br />
<br />
根据这些网络营销公司的说法，他们通过各种手法，几乎控制了国内所有的主流论坛， 对于舆论的操纵，真是做到了翻手为云，覆手为雨。<br />
<br />
某网络营销公司营销总监:“有新闻性的一些负面东西，我们可以帮你们无限扩大，我们可以在一个晚上让你们竞争的对手的消息，在网络上铺天盖地布满整个网络。”<br />
<br />
记者:“一晚上吗？”<br />
<br />
某网络营销公司营销总监:“一个晚上就可以，相当于借助他人的一个漏洞，引导着网民顺着咱们的方向去走。”<br />
<br />
那么，在这些版主把主贴发出去后，又怎么获得成百上千的网友的注意，进而营造出一个话题事件？记者在走访了多家营销公司后了解到，几乎所有网络公关 公司都必须雇佣大批的人员来为客户发帖回帖造势，这个工作常常需要成百上千个人共同完成。那些临时在网上征集来的发帖的人在行内被叫做“水军”。<br />
<br />
<br />
<br />
中科院研究生院管理学院副院长网络经济专家吕本富告诉记者，按照正常的传播规律，一个热点话题在网上的传播高峰时正常为三天，如果持续维持是违背自 然传播规律的，在三到五天后，如果突然再次出现传播的高峰，又没有特殊的原因导致旧话重提，这样的高峰就极可能是人为推动上去的。<br />
<br />
记者:“你们有水军吗？”<br />
<br />
某网络营销公司营销总监:“肯定有，你做这一行的如果给我两周的准备时间的话，我基本上可以给你集十万人，因为水军是共享的，这个行业达到今天这个效果，在论坛上，网页上，你看到地东西毫不夸张地说应该有30-40%，是这样的公司推出去的。”<br />
<br />
在群里，论坛等网络社区里，输入水军，或者网络打手等字样，进行搜索时，就会有大量的号称中国第一网络推手等的群里以及各种各样的招聘信息，记者随机加入了其中的几个群。<br />
<br />
记者:“进入这些招募水军工作的群里，我感觉简直来到了一个火爆的集市，进去以后，时不时可以看到群主发任务的通知，而且这些任务一发出来，在几十 秒甚至十几秒的时间里，就会被群里的人一拥而上全部领完，我们现在来进一步看一看，一个水军的工作程序和内容究竟是怎么样的。”<br />
<br />
记者告诉群主，自己完全是一个新手，不知道改怎么做水军，群主很快给记者发来了一个推手入门教程，做了一个短平快的培训。五分钟后，一个新手就能当 水军用了。然后记者又收到一张发帖统计表，只要记者领取了发帖任务，完成之后按照表格填清楚任务完成情况，一周后，群主就能通过支付宝付款。<br />
<br />
在能者居上的群里，记者遇到一个网名叫东琪的群主，当他得知记者已经注册了各大论坛的许多ID号时，一次性给了记者10万条的任务，要求非常简单， 就是在各大论坛回帖，并在十日内完成 。随后他还告诉记者，3天后先付1000定金，剩下的9000元20号一次结清。这样短短的十天，一个水军回复十万条帖子，就能快速赚到1万元。<br />
<br />
记者:“在跟这个叫麦子的群主，也就是一个水军小组长聊天的过程中，我在他的群共享里看到了一张工资结算表，一个水军发一条帖子，根据难易程度，可 以拿到一毛钱到五毛钱不等，我们仔细来看一下，他在这张表上登记了他所管辖的水军，相关任务的完成情况，也包括水军针对每个任务，在不同论坛的发帖情 况。”<br />
<br />
一个ID号叫镜花水月的水军，通过完成针对赤壁镇打人这个话题的回帖，在12月2号，3号两天，发帖约1571条就赚到了314元。只要有时间上 网，再加一点点耐心就可以轻轻松松，每天赚取几十到几百不等的外块，难怪有人跃跃欲试，而一些营销公司不过几个员工，就号称自己手下有十万水军，这样看来 也不奇怪了。<br />
<br />
网络营销总监:“我们可以在一夜之间铺满最少1500个论坛，在六大门户和三大社区，我们可以做出一种非常多非常集中的话题局面。”<br />
<br />
一位在一家网络打手公司工作过的前员工告诉记者，水军发帖通常五毛一条，因此行内又叫他们五毛党，这里面有学生，兼职员工或者社会闲杂人员，鱼龙混杂，但是在公司内部，却有明确的分工合作。<br />
<br />
记者:“您曾经工作过得那个网络打手公司，是什么样的一个组织架构？”<br />
<br />
网络打手公司的媒介:“比如说接到一个案例以后，是怎么样一步一步来实施到最后发给众多的水军来完成，先接到然后有策划是专门有策划的人员，然后有 项目经理，这一个项目整个的一个流程，他都要监管有人专门写帖子再交给媒介，就是我这个职位就是媒介，然后媒介看完这个帖子，他再根据这个帖子来选择论 坛，选择要发布哪些论坛，然后媒介再把选择论坛还有帖子交给下边的水军。”<br />
<br />
一个打压对手的帖子，甚至是一个夸张得变形的故事，就是这样被形形色色的网络打手包装成了众人注目的焦点。而为了这样的效果，一些客户正在不断地加入进来。<br />
<br />
记者:“你刚才说你曾经在一家从事网络打手这种业务的公司工作过，而且它在圈内还非常出名，那它都接过什么样的客户？”<br />
<br />
某网络打手公司的媒介:“那很多了，其实很多大品牌。”<br />
<br />
一个网络公关公司营销总监还得意地告诉记者，经过一系列造势，他们甚至为客户成功影响了法院的判决。<br />
<br />
网络公关公司营销总监:“期待当代包青天评判，话题的矛盾性都是非常强的，相当于在网络上造出了舆论，逼到一个风口浪尖上，国家不得不判，我做到的是让2.2亿网民全知道这事，五万元短平快，300家论坛是最核心最积累人气相当于集中了，50%网民在上面。”<br />
<br />
亿万网民，就这样跟着这些网络打手的指挥棒来贡献点击率，并在不知不觉中被引导和操纵，甚至无意中也成为打手的工具。<br />
<br />
“在行业来说很多人都是知道的，这是一个公开的秘密等于是大家都知道，只有可能一些网民不知道，他们是蒙在鼓里的，圈里人都知道，比如说谁又做了。”<br />
<br />
而这个员工曾经就职的网络打手公司，就从因为给一家客户提供所谓的全方位公关累计获取了近千万元收入。从最新的统计数据看，2009年互联网广告市场预计有200亿元规模，我们无法确切了解其中有多少收入用在了打压对手，和不正当的竞争。<br />
<br />
“发一篇新闻帖子是300元，发一篇名人博客2000元，发一个论坛帖是3000元。”<br />
<br />
记者:“这个事有必要用博客吗？”<br />
<br />
“非常有必要，博客会让我们的抓取率变得更高能挤到搜索引擎的第一页第二页。”<br />
<br />
据业界的普遍预期，在未来的5～10年之内，网络营销将会成为最有营销力的手段之一。　如果没有相关的法律来规范游戏规则，我们无法想像一个本来值得期待的网络营销产业会走向何方，又会带给我们一个怎样的网络世界。<br />
<br />
半小时观察 :网络需“打黑”<br />
<br />
互联网是一个倡导平等、自由的世界，因此很多人更愿意从网上获取信息，认为这才更接近真实。快速激增的中国网民充分地通过互联网表达出他们的意见，推动着社会文明的进步。<br />
<br />
但是，恰恰是在这个几乎是最开放、参与门槛最低的公共舆论空间里 ，有些人为了利益，不惜炮制谣言，捏造事实诋毁对手，让互联网被泛滥的谣言、谎言所充斥，更将不知情的网民控制在为自己左右的舆论之中， 而一个叫做网络打手的行业却正是通过这些病态的做法在将这一切变成产业链，“民意”成了被分食的鱼肉。<br />
<br />
面对如此众多被类似像网络口碑公司也就是所谓的“水军”、“网络打手”们控制的言论，我们想说的是网络同样需要“打黑”。让网络成为一个真实表达民意的平台。<br />
<br />
(主编:孟庆海 记者:傅喻 记者:景延 李培)<br />
