													<h2>余秋雨《钟山碑文》被指作呕</h2>
						
						
																											<div id="postmessage_360932" class="t_msgfont"><a href="http://culture.people.com.cn/GB/22219/10526012.html" target="_blank">http://culture.people.com.cn/GB/22219/10526012.html</a><br />
<img src="http://i50.tinypic.com/2j0z2fn.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
日前在中山陵梅花谷揭幕的由余秋雨撰写的南京钟山风景区碑文接连遭到学者与网民的猛烈炮轰，被骂为是“紫金山最令人作呕的一景”。<br />
<br />
　　香港《大公报》7日刊出的一篇文章称，这块自安徽运来的长12米、高4.8米、厚1.6米，重达136吨的天然巨石上，用金字阴刻着余秋雨所撰并以行书写成的碑文。南京大学历史系教授张生表示，“余秋雨的钟山碑文，刻在石头上，但我希望周围杂草速生，藤蔓疯长，将其遮盖；如果石质疏松，不久崩解，更是喜出望外。”<br />
<br />
　　张生说，我是读余先生文章长大的，实在不忍心先生注定不朽的文名在此留一遗憾。余秋雨才华迸发时不管是写苏东坡、海瑞的风雨家世，还是写敦煌卷子的流离沧桑，都令人过目难忘。但是，很遗憾，这篇碑文却几乎是写钟山的文章里面文心、文思最为贫乏的一篇。<br />
<br />
　　张生认为，盛世为文一定要千锤百炼。如今人人衣食足而知文采，普通中小学生作文令人逼视，网上写手藏龙卧虎，很多人陪余秋雨趟过文化苦旅，品过山居随笔，已非吴下阿蒙，青出于蓝而胜于蓝。强手环伺，更需要已经成名者慎重落笔。以钟山整治而言，斥资50亿，移民二万多，确实是一项超越当年滕王阁的工程，秋雨先生不能为此等文章，可以不为，为后人留一块飞白也是功德。反复表白有没有收取笔润，反而落俗了。<br />
<br />
　　自我赞叹令人莫名<br />
<br />
　　张生说，尊重前人，也就是尊重自己。自古以来，文人往往相轻，但傲视天下的李白也有服气的时候，在黄鹤楼题壁“眼前有景道不得，崔灏题诗在上头”，到了南京一带游历，坦言“一生低首谢宣城”。<br />
<br />
　　再大的文名，一定要谦虚。张生指出，古人论文，往往窥一纸而知其才。所谓“满纸风云”、“满纸烟霞”之类，本属别人的夸赞之词，但赞叹从别人嘴里出来是甜美的，从自己嘴里出来，就令人莫名惊诧了。“方落数语，已烟霞满纸，心旷神怡”，看到余先生文中末尾有这样的句子，他说，“我一定要对余先生说，这是您文章中最差的一篇！”<br />
<br />
　　付了润笔拒说数额<br />
<br />
　　南京文人学者云集，为何偏偏要请余秋雨作此碑文？据中山陵园管理局官员披露，关于“钟山碑文”，他们最初也找了当地的学者起草，甚至组织管理局内部员工撰写，“但都不满意”。后来，“管理局领导提出请余秋雨撰写”，认为一来因为余秋雨名气大，另，余秋雨多次来过钟山景区，了解钟山。于是，管理局在今年6月份派人专程赴上海，把钟山风景区背景资料送给余秋雨，余秋雨不仅“一口答应”，而且“很高兴”。中山陵园管理局对这篇碑文是赞赏有加。至于是否因此付给余秋雨报酬，该管理局官员肯定地表示“给了钱的”，但拒绝透露具体数额。<br />
<br />
　　余秋雨：是我的心声<br />
<br />
　　报道称，余秋雨在接受南京的媒体采访时表示，“这个碑文是我的真实心声。”他说，这个碑文是自己起草、自己书写，压力很大，是认真想了很久才写的。“我给很多地方题过碑，但字数都很少，字数很多的大碑文只有两个地方，一个是法门寺，一个就是中山陵了。这个碑文有两段，几百字，真的是花了心思写的。”余秋雨还说，写碑文的心得就是，对历史要有了解，要选择最重要的来写，既要小中见大，又要大中有小。“中山陵这五年来变化太大了，现在综合整治结束，到处都是相通的，非常好。我认为是大工程，大手笔。”<br />
