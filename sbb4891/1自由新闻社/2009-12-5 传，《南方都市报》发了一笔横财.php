<br />
&nbsp; &nbsp; 广州市城管委昨晚发布新闻通稿通报：针对12月4日媒体披露原市市容环卫局领导借用广日集团车辆的情况，市城管委迅速作出回应：一是要求各级各部门正确认识媒体报道，自觉接受媒体监督；二是由相关人员如实向党委说明情况并写出书面报告；三是所借用车辆不管项目结束与否立即归还；四是结合市政府机构改革的部署和要求，按照“三定”方案对原市市容环卫局、原爱卫办、原城管办等单位进行清产核资，处以上领导干部严格执行廉政制度，不得占用和长期借用企业、下属单位的车辆。对个人擅自因私借用的，依有关规定处理。五是以城管委的名义就此事专题向市政府写出书面报告。<br />
<br />
&nbsp; &nbsp; 经核实，原市市容环卫局借用车辆情况如下：<br />
<br />
&nbsp; &nbsp; 根据市政府关于由广日集团承担我市垃圾处理服务的决定，为保证垃圾处理设施的建设在移交过渡期内顺利推进，2008年底，原市市容环卫局与广日集团成立了垃圾处理设施筹建工作领导小组及筹建办公室，原市市容环卫局副局长徐建韵担任领导小组副组长。根据双方会议协定，广日集团为领导小组和筹建办公室提供两辆工作用车，车牌号分别为：粤A3000U、粤A88G67.其中粤A88G67已于今年上月归还。<br />
<br />
&nbsp; &nbsp; 此外，为推进李坑垃圾渗滤液扩容工程建设，项目总承包单位广东十六冶有限公司项目办为项目业主临时提供一辆工作用车，车牌号码为粤A 069A 6，由原市市容环卫局设施处使用，主要用于协调工程项目前期及建设过程中有关问题的解决和推进。<br />
<br />
&nbsp; &nbsp; 目前，粤A3000U、粤A069A6两辆工作用车已分别交还项目单位。</div>

							
							
							
							
