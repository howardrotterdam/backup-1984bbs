																											<div id="postmessage_429976" class="t_msgfont">贵州关岭民警处警中用枪遭暴力袭击 致两人死亡<br />
 来源：新华网 2010年01月13日<br />
　新华网贵阳1月13日电（记者李忠将、黄勇）贵州省安顺市市政府新闻办13日向媒体通报一起民警处警中使用枪支致2人死亡案件。1月12日下午，贵州关岭布依族苗族自治县公安局坡贡派出所一民警处警中使用枪支致2村民死亡。<br />
<br />
　　据安顺市政府召开的新闻发布会上介绍的情况，1月12日16时20分，关岭县公安局坡贡派出所接群众报警，称坡贡街上有人打架。接警后，该所副所长张磊立即带领协勤王道胜赶到现场处置。到达现场后，发现两名郭姓村民与另两名村民打架，民警依法亮明身份并立即对打架事件进行制止，其间，被当事人暴力袭击，民警鸣枪示警后，对方上前抢夺民警配枪，在此过程中，当事的两名村民被子弹击中死亡。<br />
<br />
　　事件引起贵州各级领导高度重视，要求查清情况，分清责任，依法依规处理并妥善做好善后工作。贵州省公安厅、安顺市及关岭县领导率有关部门赶到坡贡镇开展工作，成立工作领导小组，迅速采取相应工作措施。<br />
<br />
　　据了解，目前，民警张磊已停止执行职务，接受调查。公安部门已将案情通报关岭县人民检察院，检察机关介入开展案件调查，现场勘查、调查取证及善后工作已展开。<br />
<br />
　　截至目前，相关工作措施仍在展开，调查工作在紧张进行。安顺市政府召开新闻发布会向媒体通报案件基本情况和工作措施，并按照“客观、公正、依法、负责”的原则，做好案件调查和取证工作，尽快查清案情。</div>

							
							
							
							
