2010-01-16 09:36:05　来源: 新京报(北京)　跟贴 1515 条 手机看新闻 <br />
<br />
两个村民居然因为小纠纷，发展到了“袭警”、“抢枪”，最终被击毙。案情越是离奇，越需要公权部门给出经得起考验的调查。<br />
<br />
新京报社论<br />
<br />
1月12日下午，在贵州安顺市关岭县坡贡镇，郭家和代家两户村民因一桩亲事发生纠纷，之后警察张磊前来执法，开枪将郭永华和郭永志当场击毙。次日，安顺市政府召开新闻发布会，公布案情：警察张磊带领协勤到现场处置打架事件，其间，被当事人暴力袭击，民警鸣枪示警后，对方上前抢夺民警佩枪，在此过程中，两人“被子弹击中”死亡。<br />
<br />
新闻通报中使用了“被动语态”，避免了“警察开枪”这一说法，并且强调死者生前有“袭警”、“抢枪”的行为。依这种说法，警察开枪显得合理合法。因为《人民警察使用警械和武器条例》规定：对抢夺枪支、暴力袭警且危及生命安全的，在警告无效之后，警察可以使用武器。但这就是真相吗？谁又有权公布真相？<br />
<br />
首先，“袭警”、“抢枪”是调查结论吗？据新闻通报，目前相关领导已赶到事发地，并成立了领导小组，当事警察被停职接受调查，检察机关也正介入。但当地政府缘何先声夺人，宣布“袭警”、“抢枪”在先，警察开枪在后？如果最终调查的结论与此有出入，甚至“袭警”纯属子虚乌有，这将给调查机关带来极大压力。<br />
<br />
其次，案情的发布有仓促之嫌。此案案发于12日下午4时许，次日上午就召开了新闻发布会，更为关键的是，案情远比当地政府通报所说的复杂。<br />
<br />
有媒体引述目击者的证言，当时便衣警察张磊命令郭永华：“跪倒，否则我毙了你！”郭说：“我没有犯法，你不敢开枪。”协勤王某用电棍将其击倒后，张磊先后朝天空和地面开了两枪，接着直接向郭永华的头部开了一枪。后郭永志上前询问，张磊又朝其胯部开了一枪，之后走上前，顶着他的后脑再开一枪。<br />
<br />
但县公安局副局长否认有人身中两枪，坚称张磊只发射4发子弹。4枪还是5枪？尸检之后自然真相大白。若当事人中枪后，失去了反抗能力，警察再向其头部开枪，就涉嫌故意杀人。而且之前郭永华已被电棍击倒，再向其头部开枪，同样有故意杀人嫌疑。此外，副局长还称两次鸣枪警告后，仍然发生“抓扯”，这就与之前通报里的“抢枪”有本质区别。警察使用武器应“尽量减少人员伤亡”，对“抓扯”，为何要开枪？事关两条鲜活的生命，信息公布和警方说法理应慎之又慎。<br />
<br />
 <br />
<br />
当然，这些目击者的证言未必全部真实，那么，新闻通报里“袭警”、“抢枪”的说法又从何而来？是当事警察的一面之词吗？如果能做到起码的审慎，就应询问目击者。如果确实来不及，也不该草率公布所谓的“真相”。而且死者家属目前居然已经与镇政府签了《补偿协议》，每家补偿35万元，但必须在1月17日前安葬死者，今后不得提出任何民事赔偿。如此处理“袭警”案，又做何解释？<br />
<br />
两个村民居然因为小纠纷，发展到了“袭警”、“抢枪”，最终被击毙。案情越是离奇，越需要公权部门给出经得起考验的调查。从“躲猫猫”里的网民调查团，到上海“钓鱼执法”由记者等组成的联合调查组，再到昆明“纸币开锁”中的“现场演示”，2009年一些突发事件，都在经过了左突右支的被动尴尬之后，政府才以改错的诚意调查出了真相。“安顺枪击案”会不会重走老路？<br />
