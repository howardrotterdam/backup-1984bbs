　　“豪门会所”大学募会员<br />
<br />
　　信息时报讯 最近，一篇题为《某大学的富二代们在大学城办豪门私人会所》的帖子在论坛走红。拥有私家车、一定财富背景、学生会主席或协会负责人、车模大赛前十名……帖子公开的会员门槛引起网友们争议。<br />
<br />
　　“昨天发到寝室的传单……”楼主发完这句话后，贴了4张照片，前两张像是传单封面，上端用金色大字写着“凯乐斯私人会所――大学城里的豪门”。接着，“实力铸就经典王者绝非偶然，凯乐斯私人会所尊荣会员卡办理现正式启动”“只针对符合规定条件的会员免费办理”很是显眼。<br />
<br />
　　后两张照片特别将“会员卡办理条件”和“会员卡权利”放大。会员卡办理条件说明，只要符合以下任意一个条件即可免费办理：拥有私家车行驶证、拥有一定财富背景；有可盈利项目的负责人；学校各级学生会主席或协会负责人；有政府背景的留学生；参加唱响校园、各院校十佳歌手、车模大赛等重要文艺赛事前十名等。<br />
<br />
　　而“会员权利”和该会所所提供的“服务项目”也凸显会员“尊荣”：私人秘书服务，提供私人订餐、出行订票等服务；受邀参加会所定期举办的政商界高端聚会；受邀参加会所定期举办的明星演出……传单上还公开咨询电话和会所网址。<br />
<br />
