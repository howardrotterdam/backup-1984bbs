<br />
原题：Chinese Remake the ‘Made in Italy’ Fashion Label<br />
<br />
国际在线专稿：据美国《纽约时报》报道，意大利东部普拉托是托斯卡纳大区普拉托省的首府，向来以纺织业闻名，几百年来成了“意大利制造”的时尚之源，但市长罗伯特・切尼(Roberto Cenni)表示，来自中国的非法移民带来了噪声、恶习和妓女，让这里再也无法住人，令人厌恶，他强调将扫荡非法移民工人。<br />
<br />
中国劳工从上世纪80年代后来开始来到普拉托，起初是一小批移民后来是数以万计的涌入。在托斯卡纳大区的中心城市，中国工人从中国进口原料，然后在3200个工场中不分昼夜地生产低端服饰。意大利银行估计，在当地的中国人每天将大约150万美元的钱汇往中国，其中大部分是服装业生意赚到的，而这些钱一般不会纳税。<br />
<br />
意大利当局称，中国人及一些意大利人的有组织犯罪正在抬头，他们非法进口纺织品还从事偷渡、卖淫、赌博和洗钱等不法行为。有些当地人还称当地已经成为了“蚂蚁殖民地”。《中国围城》一书的作者西尔维亚・皮耶拉奇尼(Silvia Pieraccini)表示，意大利其他地区也密切关注着普拉托的情况。<br />
<br />
自今年春季以来，意大利当局多次突击检查中国工场的非法使用劳工情况，使得局势变得紧张，而6月底，意大利检察机关对中国100多家商铺进行调查并逮捕了24名中国人，使气氛更加紧张，检察机关指控他们涉嫌洗钱、卖淫并将外国产品贴上“意大利制造”标签。<br />
<br />
《纽约时报》称，中国人重制了“意大利制造”的标签，据普拉托市政府统计，该市18.7万名居民中，中国非法移民就有1.15万人，但官方估计，实际可能还存在2.5万名主要来自中国的非法移民。罗伯特・切尼本人是纺织业的大股东，由于当地人不满外国移民到当地抢饭碗，因此市长罗伯特・切尼决定扫荡非法移民，像今年就已经有多家中国工场遭突击检查。(关谷)<br />
