																											<div id="postmessage_182640" class="t_msgfont">摩罗：爱国之前人类爱过什么？<br />
<a href="http://blog.sina.com.cn/s/blog_4aff84920100dasu.html" target="_blank">http://blog.sina.com.cn/s/blog_4aff84920100dasu.html</a><br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;爱国精神只是自爱精神的一种折射。每一个生命不可能不自爱，每一个自爱的生命不可能不爱自己的社会群体。从蚂蚁、蜜蜂到猿猴再到人类，都是这样。国家只是人的群体认同和社会认同的诸多载体之一。由于在人类历史长河中，国家是很晚起的事物，所以，爱国主义是人类精神文化中一种比较新生的思潮。 <br />
&nbsp; &nbsp; 那么，爱国之前，人类爱的是什么？ <br />
&nbsp; &nbsp; 人类最早的群体是共同采集和狩猎的游群，每个个体都必须依赖同一游群的相互帮助，用以加强生存保障。根据动物学家对于现代猿猴和哺乳动物群体的研究，游群虽然以血缘关系为基础，但是也有一定的随机性。成年以后被猴王或狮王驱赶的雄性猴子和雄性狮子，可以单独游荡，也可以加入到其他群体中抢占王位。人类氏族社会出现以后，单系（或者父系或者母系）血缘关系作为社会共同体的组织原则更加坚实，每个氏族跟与之通婚的其他氏族组成一个部落。氏族和部落乃成为原始人最主要的共同体。氏族时代通行共产主义制度，每个氏族是一个独立的生产单位，所有的采集所得和狩猎所得，由整个氏族全体人员共同享用。在这样的社会组织中，氏族成员不爱自己的氏族是不可能的。 <br />
&nbsp; &nbsp; 氏族不但是血缘共同体和生产共同体，也是军事共同体。美国学者摩尔根说，一个氏族跟本部落之外的其他氏族，在理论上永远处在战争状态。只要某个氏族中某个人（不限于酋长等首领）觉得需要到其他氏族中掠夺一些东西，并能纠集到几个好汉同行，那么一场厮杀和劫掠马上就可以“合法”地开始，谁也不觉得这有什么突兀，更没有什么人道主义谴责。氏族是所有人共同的社会依托，如果氏族遭遇强敌的屠杀，幸存者就将沦为强敌祭坛上的祭品。所以，每个人都会自觉地捍卫自己的氏族。可以想见，那个时代的“爱氏族主义”，比后起的“爱国主义”要强烈很多倍。 <br />
&nbsp; &nbsp; 随着一些氏族和部落的强大，他们所能征服的地区和人群越来越多，他们的社会控制和管理能力也不断增强。强者不需要亲手抢劫，而是规定那些被征服的氏族定期进贡，氏族与氏族之间、部落与部落之间的社会交往愈益频繁，文化、宗教和政治等方面的认同也越来越广泛。于是，超越氏族和部落的共同体逐步显现。中国大地上良渚文化时期的酋邦，地中海地区雅典、斯巴达等等城邦，都是规模远远超过氏族和部落的政治共同体。那个时代，“爱氏族主义”发展为“爱酋邦主义”。<br />
&nbsp; &nbsp; “爱酋邦主义”为什么能够巩固下来？因为人类的交往范围越来越大，社会组织也越来越细密，个体生命的视野越来越开阔。所以，作为人们社会依托的政治实体也就越来越大。人们不再局限于站在氏族里边提防其他氏族，而是站在酋邦里边提防着其他酋邦。<br />
&nbsp; &nbsp; 氏族时代，人类社会分群的第一条界限是氏族。酋邦时代，人类社会分群的第一条界限是酋邦。 <br />
&nbsp; &nbsp; 一万年前，地球上类似于酋邦的政治实体可能有十万个。经过漫长的社会发展，我们今天所面对的世界，是由大约两百个大大小小的国家组成的。人类社会分群的第一条界限，相应地由酋邦发展为国家。每一条界线所圈定的范围，也越来越广阔。人们所赖以生存并赖以给自己身份定位的第一个事物，就是国家。最近北美洲爆发传染病甲型流感，中国政府派专机到墨西哥疫区将特定的几百人接回中国，中国政府和同胞之所以最关心这几百人的生命安全，仅仅因为他们是中国人，跟我们具有民族和国家认同。这一事件和行为将国家是人类分群的第一原则阐释得清清楚楚。在这样的人类分群模式中，国民将国家看作与自己生命和利益相关的人文价值之一部分，乃是顺理成章的。在这样的文化背景中，一个人不爱自己的国家是不可思议的。 <br />
&nbsp; &nbsp; 可见，爱国主义是人们自爱的一种表现形式。它从“爱氏族主义”和“爱酋邦主义”发展而来，非常自然，非常顺理成章。就像自爱是一种无可厚非的生命本能一样，爱国主义也是一种无可厚非的本能的情感。 <br />
&nbsp; &nbsp; 但是，爱国主义发展到极点，也就难免出现一些悖谬之处。各种集体（无论是氏族、酋邦、城邦还是国家）也像每个个人一样，对于利益的追求是无止境的。如果过分追求自身的利益，就难免对他者的利益构成伤害。可是，每种发展到极致的人类品质都是非理性的，爱国主义也是一个没有限度的价值观，它只能是一把双刃剑。一个氏族成员从另一个氏族劫掠的财产越多，就越是被自己的氏族奉为大英雄，可是对于被劫掠的民族来说，他却是一个恶魔。成吉思汗对于汉族人、斯拉夫族人来说无疑是杀人魔王，对于蒙古族来说却是空前的英雄。伊藤博文、东条英机对于中国人来说，一个是逼迫中国签订不平等的《马关条约》的奸雄，一个是罪恶的刽子手。可是对于日本人来说，他们都是国家的圣人和功臣，因为他们能够从外族掠夺无穷的财富和光荣，奉送给他的同胞，所以日本人世世代代将他们的灵位供奉在靖国神社，长期祭拜。华盛顿、杰弗逊对于美国来说是立国圣人和英雄，可是对于英国来说他们叛徒，对于印第安人来说他们则是施行种族灭绝大屠杀的刽子手。 <br />
&nbsp; &nbsp; 五百年来无数欧洲人对全世界所有其他民族和国家展开殖民大屠杀、大掠夺，无数尸骨和财富养肥了欧洲国家和国民，他们是这个地球上最早的爱国主义者，可是对于被殖民、这屠杀、被掠夺的民族来说，他们个个都是魔鬼。 <br />
&nbsp; &nbsp; 为什么我们认为殖民者是魔鬼？因为我们也是爱国主义者，我们也爱我们的国家。一切损害我们国家和国民利益的强盗，都是魔鬼――从成吉思汗到努尔哈赤，从鸦片贸易的商贩到八国联军的军人，到侵华日军的官兵。 <br />
&nbsp; &nbsp; 在国家成为人类社会分群界线的第一原则的时代，功臣与罪人、圣人与魔鬼的区分标准，与个人品德全无干系，仅看他谋求的是谁的利益，损害的是谁的生命。这个标准简单其实就是“爱国主义”。</div>

							
							
							
							
