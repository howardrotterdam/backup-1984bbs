													<h2>摩罗向左转：爱国主义为什么遭到围剿</h2>
						
						
																											<div id="postmessage_182520" class="t_msgfont">摩罗：爱国主义为什么遭到围剿<br />
<a href="http://blog.sina.com.cn/s/blog_4aff84920100ehy4.html" target="_blank">http://blog.sina.com.cn/s/blog_4aff84920100ehy4.html</a><br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;2009年上半年，中国图书市场先后出现了《中国不高兴》（作者为宋晓军、王小东、黄纪苏、宋强、刘仰）和《中国没有榜样》（作者为刘仰）两本书。这两本书的作者比较强调在全球化背景下中国的国家认同和国家利益问题。《中国不高兴》两个月内就发行了六十万册，说明深受读者欢迎。但是中国的主流媒体对它们一片骂声，其中有一篇文章批评得特别严厉，文末企图寻找一个最有分量的词用以概括这两本书的恶劣品质，作者所选定的这个词是“民族主义”，可见“民族主义”在这些作者的心中接近妖魔鬼怪。 <br />
&nbsp; &nbsp; 很多中国人并不知道民族主义对于中国来说究竟是有利还是有害，只知道既然西方人说了民族主义不好，咱们就不应该再喜欢。 <br />
&nbsp; &nbsp; 那意思是，咱们这种劣等民族，就不配讲什么民族主义。 <br />
&nbsp; &nbsp; 一百年来中国学术界几乎没有自己的话题，所有的命题和思想乃至于观点都是从西方引进的。对民族主义的妖魔化描述正是来自西方世界的文化现象。民族主义以及与此相关的国家主义、爱国主义，本是近代以来西方列强配合其民族国家运动而建构的意识形态。借助国家主义思潮，欧洲大地上原先经常混战不休的政治实体逐渐划清了政治和利益的边界，从而形成了今天所谓法国、德国、荷兰、奥地利、意大利、英国等等民族国家单位。 <br />
&nbsp; &nbsp; 在他们各自强调自己民族国家利益的过程中，各国之间产生了严重的政治斗争和军事冲突。 <br />
&nbsp; &nbsp; 欧洲人面对两次世界大战造成的废墟，痛定思痛，认为这是国家主义和民族主义思想带来的悲惨结果。第一次世界大战结束以后，欧洲知识界就开始了对于民族主义的否定和清算。二战以后这种否定和清算尤为坚决。 <br />
&nbsp; &nbsp; 汤因比就把民族国家理论看作西方文化的毒瘤，它所造成的第一个恶果是通过两次世界大战将西方世界推向毁灭的边缘。第二个恶果看来是欧洲之外的地区和白人之外的种族纷纷接纳民族国家理论之后，也在自己的社会力图建设民族国家，并因此引起无休止的地区纷争和国际纷争。 <br />
&nbsp; &nbsp; 西方人有意降低甚至否定国家认同的人文价值，固然含有真诚反省的一面，但是，我们可以大胆地指出，也有通过消解其他地区居民的国家认同，进而为新的殖民掠夺扫清障碍的用意。 <br />
&nbsp; &nbsp; 在西方强国仰仗民族国家理论，为了满足国家利益而肆意掠夺全世界的财富，进而将自己建设成了世界上最为强大的霸权国家之后，被殖民地区正是借助同一个民族国家理论，建设起新的国家组织，从而纷纷摆脱西方殖民强国的控制与奴役。 <br />
&nbsp; &nbsp; 对于广大的被殖民的东方地区而言，民族国家理论乃是一个尚未失去正面价值的有效学说。西方国家将族国家理论妖魔化，实际上包含着对于东方主权国家的政治价值和人文价值之消解。 <br />
&nbsp; &nbsp; 在降低国家认同的人文价值的过程中，西方国家抬出世界主义、国际主义等等概念用以取代。在实际文化运动之中，世界主义、国际主义并没有产生多大的影响，倒是对于西欧地区各个国家及其居民在文化上、种族上的认同与统一，起到了引导作用。它的现实的成果就是欧盟的出现。 <br />
&nbsp; &nbsp; 二战结束以后，政治与军事殖民的漫长历史告一段落，为了在新的语境中持续他们的殖民统治，巩固他们的殖民成果，维护他们在五百年殖民历史中形成的霸权地位，一个超越“国家”概念的、具有文化和种族同一性的概念出现了，这个概念叫做“西方”。欧洲和美国迅速抱作一团，他们依然是世界的老大。他们扮演着弱势国家保护者的形象，这种形象只是殖民时代宗主国形象的翻版，他们各式各样的跨国公司、金融机构以及形形色色的国际组织，像毛细血管那样细密、严谨地伸向世界各个角落，实现着对于全世界财富、资源的控制和攫取。 <br />
&nbsp; &nbsp; 对于西方强国的跨国公司来说，他们到东方世界进行掠夺的第一个也是最主要的障碍就是那些地区的国家政权，只有淡化了弱势地区的国家主权，他们才能减少掠夺的障碍和代价，从而实现利益的最大化。 <br />
&nbsp; &nbsp; 美国学者亨廷顿在《文明的冲突》中，强调作为整体的西方文化与世界上其他文化类型的（例如伊斯兰文化、中国文化等）的区别、对峙与矛盾，并预言日后世界上的冲突与战争主要发生在不同的文明之间。这种解释框架的变更，一定程度地掩盖了西方社会对于国家利益的追求，尤其掩盖了他们为了自身国家利益而肆意践踏和掠夺其他国家的国家利益以及公民利益的真相。好像世界的纷争不是因为西方国家的掠夺引起的，而是不同文明的不同观念引起的。这显然是一种欺人之说。 <br />
&nbsp; &nbsp; 一百年来，中国精英群体一直跟着西方国家鹦鹉学舌，所有的学术命题都照搬西方。西方人吃饱了，中国精英就忙着打饱嗝，西方人感冒了，中国精英就忙着咳嗽，西方人在某个方面走得过火了，中国精英就马上跟着拉肚子。 <br />
&nbsp; &nbsp; 民族国家理论甚嚣尘上的时候，中国精英还浑然不觉。等到西方的大炮把中国彻底轰倒，中国精英慢慢醒过神来，决定要拜倒在这个强盗脚下，从此跟着那一群血迹斑斑的强盗亦步亦趋。 <br />
&nbsp; &nbsp; 在五四运动前后，世界上被压迫民族借助民族国家学说，掀起了一个摆脱殖民统治的高潮。西方学者当然要拿着世界主义、国际主义的学说，来给这种民族自治要求和爱国精神灭火。中国的精英学者也跟着猛拉民族主义和爱国主义的肚子，大谈民族主义、爱国主义的弊病，大谈国际主义和世界主义的神圣，就像今天的精英高度亢奋地大谈全球化一样――他们根本不想想，当时的国际究竟是谁的国际？当时的世界究竟是谁的世界？今天的全球化究竟是谁的全球化？ <br />
&nbsp; &nbsp; 中国本来就是一个爱国主义资源极为稀薄的国家，近代以来处于欧美和日本这些民族主义疯子的重重包围之中，长期面临着灭国的威胁。 <br />
&nbsp; &nbsp; 中国的精英群体却不知道适时地利用西方社会出现民族主义疑虑的国际背景，好好鼓动国民热爱自己的国家和文化，以便适时地强化国家认同和民族凝聚力，在虎狼环伺的险恶境遇中打开中华民族的生存空间。 <br />
&nbsp; &nbsp; 与此相反，中国精英群体竟然跟着西方学者盲目嚷嚷着世界主义之类，同时对自己的传统文化发起猛烈的、毁灭性的攻击，还自以为这样就是站到了国际前沿，就跟那些伟大民族的伟大学者同样伟大了。这是断送中国前程的愚蠢行为。 <br />
&nbsp; &nbsp; 中国的战国时代曾经发展起爱国主义，屈原沉江、晏子使楚、郑国牛贩子弦高智退秦师等等爱国故事成为千古美谈。可是，秦汉建立大一统帝国之后，周边没有文化水平和政治建制相对等的国家做对应物，国家的观念在中国人的心中日渐淡薄，天下观念却日益发达。<br />
&nbsp; &nbsp; 《礼记》描述君子的志向和职责，不以治国为终点，而是直指天下。国家只不过是人类分群的一种组织模式，天下则指全人类的人文存在。天下胸怀，非爱国主义所可比拟。 <br />
&nbsp; &nbsp; 不独精英群体乐于以天下胸怀包容家国之志，连普通居民也不以国家为意。南宋时期，面对被异族侵吞的大片北方国土，江浙的居民却反对大动干戈收复失地。他们关注的是过好自己的日子，不愿意看到政府以收复失地的名义课以重税。这种选择显示了国家认同在居民心中占有的地位不是很高。 <br />
&nbsp; &nbsp; 早在十六世纪，来华传教的利玛窦就敏锐地发现，中国从来不以国家的名义跟周边社会打交道。那些小国来中国进贡，中国的心态不是“交易”而是“交往”。中国朝廷赏赐大量的财物，作为贡品的回报。如果用西方贸易的眼光看待，中国朝廷颇为吃亏。可是中国的“贡赋贸易”体系本不是一种“国际交易”模式，而是一种“天下交往”模式。 <br />
&nbsp; &nbsp; 后来的一些西方学者更明确地认为，中国不是一个国家，而是一种文明。中央政权跟它的臣民及众多藩属国组成一个文明体系。这跟中国近代学者的认识基本一致。梁启超在《爱国论》和其他许多著作中都表述过这个意思，在中国无法以独立国家的名义自立于世界的那个悲惨时代，梁启超的这种描述充满了无奈。 <br />
&nbsp; &nbsp; 事实正是如此，在中国人民备受凌辱的整个近代史上，满清权贵就一直没有把国家利益放在重要的位置上。他们始终把诺大的中国看作他们的狩猎场，以汉族为主体的各族人民始终是他们的最大对手，而不认为他们跟汉族是一个国家共同体。他们在惨遭失败之后，不得不“量中华之物力，结与国之欢心”，也就是把汉族等等猎物让出一部分给西方强盗。 <br />
&nbsp; &nbsp; 满族也是中华文化圈的一部分，他们的心态，一定程度地体现了中国民众国家意识和国家认同的匮乏，离爱国主义似乎相去甚远。 <br />
&nbsp; &nbsp; 由于近代以来反复遭遇西方列强的屠杀、劫掠和各种形式的奴役，精英群体中少数杰出人物通过反复的学习和思考，终于逐步建立起国家观念，滋长起强烈的爱国激情，并投身到保国保种的斗争中。第一次世界大战爆发后，梁启超、梁士诒等爱国人士主张通过参战改变中国卑屈、孱弱的国际地位。可是世界霸主英国坚决拒绝中国参战。袁世凯当局在英法联军战局失利的情况下，要求由中国派工人（而不是军人）到法国战场从事后勤工作，才被应允。 <br />
&nbsp; &nbsp; 十四万五千华工为协约国的胜利牺牲了两万多人。中国因此而成为二十七个战胜国之一。可是，中国人民的鲜血并不能博得西方强国的尊重。 <br />
&nbsp; &nbsp; 巴黎和会上，中国作为战胜国竟然没有权利收回被战败国德国占领的青岛等等领土，世界霸主们决意将德国在中国的权利移交给恶魔国家日本。 <br />
&nbsp; &nbsp; 中国以战胜国的身份受到这般凌辱，激发了中国国民强烈的爱国心。1919年五月，由中国学生、中国工人（包括从一战战场返回的华工）、中国商人、中国市民共同参加的游行、罢课、罢工、罢市运动，标志着中国民众国家意识和爱国精神的觉醒。这次后来被命名为五四运动的爱国运动受到少数杰出人物的支持，成为日后中国一度彻底摆脱西方殖民者控制和奴役的滥觞。 <br />
&nbsp; &nbsp; 五四青年群体爱国精神的觉醒，与精英群体中少数杰出人物的爱国主义思想相呼应。 <br />
&nbsp; &nbsp; 1902年，梁启超在撰写《论中国学术思想变迁之大势》的开头，热情澎湃地说：“於兮，美哉我国！於兮，伟大哉我国民！吾当草此论之始，吾不得不三薰三沐，仰天百拜，谢其生我于此至美之国，而为此伟大国民之一分子也。……吾欲草此论，吾之热血，如火如焰，吾之希望，如海如潮。吾不自知吾气焰之何以喷涌，吾手足之何以舞蹈也。於兮！吾爱我祖国，吾爱我同胞之国民！” <br />
&nbsp; &nbsp; 在国将不国的苦难时期，梁启超始终跟这个苦难国家相依为命。尽管他是最系统地学习西方文化与产业、科技的最早提倡者之一，尽管一战以后他也发表过许多阐述世界主义的文章，可是梁启超从来不曾为了追赶西方的时髦，而把自己置于国家民族之上，从来不曾以导师名义对自己的国家民族指手划脚。他始终把自己置于这个苦难民族的群体之中，置于这种伟大文化的滋养之中。 <br />
&nbsp; &nbsp; 若是其他精英人物对于国家民族怀有“三薰三沐”十分之一的庄严感和虔敬心，也不至于用那种畸形的方式诅咒自己的国族和文化。 <br />
&nbsp; &nbsp; 梁启超很可能是着意激发中国国民的国家意识和爱国精神的先驱。他对于中华民族及其文化的真挚感情，他对于中国同胞的深切热爱，今天读来依然禁不住热血奔涌。 <br />
&nbsp; &nbsp; 以梁启超为先驱、以五四运动为标志的爱国主义精神的觉醒，完全是在殖民帝国的长期掠夺和凌辱中，激发起来的自卫反应，我们不得不被迫爱国。 <br />
&nbsp; &nbsp; 可是这种“被迫爱国”的觉醒，却没有在日后的中国一以贯之，而是经历了异常曲折的发展：一会儿彻底断裂、一会儿游丝一线、一会儿突然高涨、一会儿竟然作为负面价值受到精英群体的围剿和扫荡。 <br />
&nbsp; &nbsp; 自从二十世纪八十年代以来，中国的精英群体竟然因为当年中国没有像印度和香港那样沦为完全的殖民地而痛苦得寻死觅活。他们对于五百年殖民史上所有血淋淋的事实一笔抹杀，他们对于西方列强用四个士兵的阵亡造成中国三万士兵牺牲的残酷屠杀完全视而不见。 他们决定奋起直追，超越时空限制，跪下来请求殖民者的后代将中国重新纳入任由他们控制和抽血的轨道。 <br />
&nbsp; &nbsp; 在他们生起这个愿望的时候，爱国主义自然受到压抑，在他们高歌猛进的时候，爱国主义受到空前的围剿。 <br />
&nbsp; &nbsp; 如果哪个少不更事的年轻人面对西方人的暴行（比如轰炸中国大使馆）稍稍表示一下不高兴，他们说这是“愤青”，要是有人胆敢在国际社会中表达了一点维护中国利益的愿望，他们说这些都是“爱国贼”。 <br />
&nbsp; &nbsp; 中国人的爱国本来是西方人教给我们的，但是一些中国精英说：我们不配！他们天天羡慕西方人的爱国，同时天天祈祷西方人连我们的国家一起爱。可是，西方那些高贵的国家，过去他们只爱我们的白银，并天天琢磨瓜分中国的方案，今天他们只爱免费享用我们的一切产品，只爱我们为他们的穷奢极欲买单，并天天琢磨如何分裂我们的国家（美国兰德公司所挂的中国地图上，西藏、新疆、内蒙都不在中国版图之内，更别说台湾了）。 <br />
&nbsp; &nbsp; 尽管如此，少数中国精英依然只允许我们将自己的国家送给西方人爱，我们自己一爱，就被他们命名为“爱国贼”。 中国于是进入了一个公开围剿“爱国贼”的畸形时代。 <br />
&nbsp; &nbsp; 这种畸形现象，显示了中国不同群体在文化认同、国家认同方面的分裂。这种分裂将给未来中国带来什么样的影响？也许是值得关注的问题。</div>

							
							
							
							
