<br />
这些年随着经济腾飞，中国综合国力迅速提升，在国际间话语权与日俱增，但是总的而言，中国在不少外国人眼中只是一个暴发户，未能得到应有的尊重。个中原因很多，政治体制不够民主、价值观迥异等耳熟能详，我们今日不探讨这些大道理，只是由上海世博会试运行到正式开园所见种种乱象，中国的管理水平和国民质素之低落，使人感到“与有耻焉”。中国离值得尊重的大国、中国人离值得尊重之路，还很遥远。 <br />
　　游人抢预约票大混战 外国记者看得阴阴笑 <br />
<br />
　　上海世博会开园3日以来，最使人印象深刻者，是每日上午9时游人进场时的场面，但见大批游人同时间狂奔，然后围?派发中国馆入场票的工作人员哄抢门票；游人不分男女老嫩、相互叱?、推撞、抢夺大混战，战况之激烈，使人看得目瞪口呆。这就是正在崛起的中国国民的表现。许多外国记者和香港记者一起采访这一幕，由于昨日已经是第3日混战了，有香港记者注意到外国记者对眼前战?“咪咪嘴”讪笑。我们可以这样说，透过外国记者报道抢门票这一幕，肯定挫损了中国和中国人的形象。 <br />
<br />
<br />
　　这一幕，一手编导乱象的世博会管理当局，应该负上主要责任。中国馆是明星馆，备受关注，中国人到世博会都想参观中国馆，因此，参观中国馆人数?多，应该早在预期之内，当局本来设计以机器预约门票，但是从试运行到正式开园，机器都未能发挥功能，当局改用人手派发门票。 <br />
　　但是，派门票的方式形同儿戏，由可以随意走动的工作人员拿?门票派发，工作人员面对数以千计的游人涌来，被推得东歪西倒，有时为稍为确保自身安全而移动，这样的话，工作人员就宛如移动的标杆，游人随?标杆移动哄抢，发挥擅长的争先恐后本领，哪有不乱之理？而游人排队苦候数小时才得以进场，门票约10分钟左右就派完了，不鼓噪者，稀矣！ <br />
<br />
　　过去3日，究竟有多少中国馆门票可供派发？据知中国馆每日可以接待约5万人，其中约1.5万张预约票给团体或旅行社，即场派发的每日超过3万张，很难想像10数分钟之内就派了那么多门票。其实，除非有不可告人情事，每日有多少门票可供派发，并非什么“国家机密”，理应公告周知，让游人心中有数，而且在有控制下派队依次派票，则游览世博就不会被形容为一场艰苦的斗争了。 <br />
<br />
　　上海是内地的先进城市，管理世博的肯定是精英一族，他们怎么连排队派门票的概念也没有，偏要制造哄抢场面，实在使人百思不得其解。管理当局必须尽快改善情?，不要让“丑陋的中国人”继续在世人面前丢架。 <br />
<br />
　　世博会管理当局的一些做法，与“以民为本”相去甚远。例如，其他国家设置长者优先通道，使长者免于排队之苦，这个敬老安排中国馆原本未顾及，不过不要紧，只要见贤思齐，立即仿效，未尝不是一项德政。昨日，年届70岁长者的优先进中国馆的通道辟出来了，但是事前并无公布，馆前又无指示牌引导游人；另外，只容许一人陪同长者进馆，若一家几口游世博，就要分道扬镳而游兴大减。当局只学人家的方法，却未能领略其神髓，这是长官意志的体现，也反映管理水平尚待提高。 <br />
<br />
　　内地民?插队争先，缺乏公德心等劣行，许多人都领教过，连日来记者在世博园区所见，民?的行径肯定未能为自己和中国的形象加分。例如，园区内许多空间都成为游人的休憩处，记者所见，他们不管三七二十一，随意席地而坐、铺开园区地图脱去鞋子就睡觉，不管观瞻，不理仪态，一副我行我素的所为，使人不知道从何说起。 <br />
<br />
　　据记者观察，民众在园区横七竖八，除了涉及个人教养，可能与园内缺乏长椅有关。管理当局为免长椅阻碍人潮前进，拆走了不少长椅，园区大，连日温度又高，在园区奔波，确实够累的，游人唯有自行找休息之处了，一些内部空间较大、有摆放长椅的场馆如荷兰、瑞士馆等，就成为游人的“营区”，坐在馆内就不走了。所以，游人在这方面之不自重，也与管理当局有关。 <br />
<br />
　　因应民众质素与国情 以妥善管理引导自重 <br />
<br />
　　上海世博会的主题是“城市，让生活更美好”，能否成功演绎，有诸般因素；不过，若把世博园区视为上海或城市的缩影，则连日来予人的观感，肯定与这个主题的愿景存在较大落差。 <br />
<br />
　　中国首次办世博，要中国民?一下子提升质素，做到“文明观博”，不切实际也不可能，但是这样的游人质素，对于管理当局来说是挑战、也是立功的大好机会。因为中国民众的质素和“国情”，当局应该知之甚详，以妥善的安排引导游人，让游人不知不觉中守秩序、守规矩，果能如此，则中国和中国人的新精神面貌，就会呈现在世人面前。世博展期还有180日，希望在管理当局努力、民?配合下，中国和中国人透过世博扭转世人的既定认知，赢得大家所渴求的尊重。</div>

							
							
							
							
