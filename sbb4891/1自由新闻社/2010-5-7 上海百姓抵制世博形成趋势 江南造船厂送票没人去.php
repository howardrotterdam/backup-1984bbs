<br />
5日上午，天空就开始淅淅沥沥地下雨。雨水果然阻挡了不少游客的步伐。上午9时30分，常规的入园高峰期，世博园的入园人数仅为1.9万，创下历史新低。同一时间段，前一天的入园人数是7万人左右，为5日的三倍之多。<br />
<br />
再到园区，前几天人来人往的街道果然冷清了不少。上午10时，人气最旺的C片欧洲区，首度未见排队长龙。去加拿大、美国、智利、墨西哥、俄罗斯等馆，完全不费功夫，无需排队。而就连德国馆、法国馆及英国馆这三大人气王，昨日的排队阵容也大大缩水，不足百米，游客基本可鱼贯而入。最长的等候，也不过10分钟，与之前的两三个小时，有着天壤之别。<br />
<br />
观光人数锐减 世博会尴尬<br />
<br />
据自由亚洲电台报导，根据世博官方网站6日的页面显示，世博开园前5日，累计人数79.6万人次，有网友夏勇正（音）还发现，官方网站的入园统计改人工发布而不是自动呈现，本来每隔半小时的几个即时统计，在6日全部撤掉，改为傍晚17:30 统一公布，网友认为这显示官方已经对过低的参观人数感到窘迫， 于是不敢将数据即时而直接地呈现在大众面前。<br />
<br />
根据统计，在“五一”劳动节3天的指定日中，仅有55万人进入世博园参观，几乎是世博开幕前人们预期的一天参观的人数。。此前，在世博会门票预售统计中，“五一”劳动节3天的指定日票，是每日售出约35万张。3天过去，相当于有一半购票者放弃首发参观权。<br />
<br />
世博局局长洪浩5日表示，入园人数低于预期，184 天的会展期间总接待游客量可能只及原来估计的不足一半。世博局对本次世博会的预期是7000万人参访，如以目前的情况测算，将只有3312万人，远低于会前预期的目标。<br />
<br />
<br />
世博会观光人数锐减<br />
<br />
外界质疑刺激措施效果有限<br />
<br />
上海当局近日给本地居民家庭发放世博礼包，包括家庭门票和200元面额地铁票，并根据区县进行分片、分时组织前往参观。这样预计可能为世博会带来 1000万到2000万的访问增加量，另外各个省市也可能会支援上海，作出相应的团体组织参观行动。但是外界质疑当局如此对应策略，是否会带来预想效果也是一个疑问。<br />
<br />
香港时事评论员周滨对此表示，“世博会人潮减少，有各种原因，一个原因是大家看到世博会人潮汹涌，在这种情况下，有些原定在五一长假要去的可能就却步了，另一个，就是它的公共关系工作做得也不好，国外发达国家的体制里面有一个很重要的目的是服务，因此服务方面有很多经验，中国体制的特点，什么事情都是管理和控制，这个出发点不一样，做法就不一样，而且想到的那些层面就不一样，这是一个重大的差别。<br />
<br />
有网友在微博写着“人少，园空，速来！”开园仅仅6天，虽说世博园区客流规律还难以捉摸。但与试运行期间的“火热”相比，世博园甫一开园，客流“退烧”明显。<br />
<br />
网友circusee讽刺的表示，“只要统计局出马，7000万不成问题”，但是在举世瞩目中，虽仍然可以做一些掩饰，但是绝对造假已经很难。<br />
<br />
著名博客作者郭卫东表示，世博会打从开幕前就开始造假，“这个世博会像山寨世博会，本身这次像中国馆的设计，还有世博会的主题曲，还有世博会的标志(吉祥物)都是模仿其他国家的造型，这种听起来就是比较耻辱的事情，一个世博会怎么会搞得草木皆兵呢？”</div>

							
							
							
							
