													<h2>四川外语学院排练8国语言诵读《共产党宣言》</h2>
						
						
																											<div id="postmessage_799436" class="t_msgfont">2010-08-27 05:43:00　来源: 重庆晨报(重庆)<br />
<br />
<img src="http://img1.cache.netease.com/catchpic/4/40/408693A20251BB06A02438EFF257A380.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
激昂的音乐下，舞台上50多名同学铿锵有力的大声诵读着：“现在是共产党人向全世界公开说明，自己的观点，自己的目的，自己的意图，并且拿自己的宣言，来反驳关于共产主义幽灵的神话的时候了……”<br />
<br />
　　中文、英语、法语、德语、俄语、意大利语、丹麦语、弗拉芒语，8种铿锵有力、气势磅礴的语言，分别从8个方阵中爆发出来，响彻在舞台的每一个角落。<br />
<br />
　　这是四川外语学院排练8国语言诵读《共产党宣言》选段的现场片段，这已经成为“唱读讲传”的经典节目之一，川外这个团队也成了唱读讲传活动中涌现出来的明星团队。<br />
<br />
　　全国寻弗拉芒语专家<br />
<br />
　　用8国语言诵读《共产党宣言》，这个节目看着就不容易，但却很具有川外特色。“英、法、德、俄、丹麦、意大利语这几种语言，我们找学校自己的专家教授来指导。”川外团委书记丁欢是节目导演，她请教授先把《共产党宣言》几个语种版本找好，然后一个单词一个音标的给同学指导，有语言基础的同学在教授指导下学起来都特别带劲，没用太长时间，大家就都学会了。<br />
<br />
　　最难的却是弗拉芒语，这个语种让负责节目编导的丁欢忙了好几天，会这种语言的专家在全国少之又少，要在重庆找到这样的专家更是困难。<br />
<br />
　　“最后我们学校决定向全国求教，找一个弗拉芒语的专家来指导。”丁欢现在还记得那时的困难情景，皇天不负苦心人，经过不懈的努力，还真让丁欢在北京找到了一位懂弗拉芒语的专家。<br />
<br />
　　通过沟通，专家把《共产党宣言》（选段）弗拉芒语版本的每一个字、每一个发音通过录音录下来，然后从北京寄到了重庆。<br />
<br />
　　拿到录音，大家就开始围着录音设备转了起来，mp3、mp4、手机，每个人都运用自己的装备学习弗拉芒语，早上醒来、晚上睡觉前，甚至吃饭的时候，都在学习弗拉芒语，只为在最短的时间里熟悉它。<br />
<br />
　　能用三种语言来表演<br />
<br />
　　经过节目的锻炼，参加节目的每个人都成了语言达人。“我们现在每个人都至少能用3种语言来表演这个节目。”丁欢谈起这些大学生时满脸骄傲。<br />
<br />
　　说起表演，所有同学都显得特别兴奋，争着谈自己在节目排练中的感受。大三法语系的龚书为了角色需要，去年就开始学起了意大利语。“刚开始学的时候练习弹舌最难，我做梦都在练，刚开始那几天晚上都不敢睡觉，害怕第二天把弹舌的感觉忘了。”就这样，龚书把意大利语的感觉找到了。<br />
<br />
　　大家感受到团队精神<br />
<br />
　　“我们有一个特殊的训练方法，效果相当好。”丁欢一脸神秘的说，让一个方阵分成2队，比赛赢了的要替输了的同学受惩罚。惩罚的项目是下蹲起，大家也给这样的惩罚取了好听的名字：苹果蹲、芒果蹲。<br />
<br />
　　“我们的同学特别的善良，虽然是练习的游戏，但是他们也不忍心其他人因为自己受罚，所以特别努力认真，很快就熟悉了台词。”在轻松的氛围中，丁欢老师和大家一起度过了愉快的训练时光，每个人都在游戏中感受到了团队精神。<br />
<br />
　　“这种方法让我们能用好几种语言来诵读《共产党宣言》上面的内容，我现在就会3种，表演排练成了我们学习其他语言的课堂了。”川外国际关系学院的罗茜从节目中学到了很多。<br />
<br />
　　“现在他们几乎都能担任自己角色以外的其他语言角色了。”虽然训练时对大家很严格，但大家都把丁欢看做大姐姐，每次听到这个称呼，她都会笑起来，“我其实也是大家的保姆。”<br />
<br />
　　参演者主动申请入党<br />
<br />
　　“原来的德语领颂徐萌已经出国了，现在的德语领颂是英语系的大三男生钟欣瑞，他的德语也很不错。”大学生们换了一茬又一茬，但这个节目却一直保持着高质量，在排练和演出之后，这些80后、90后的大学生也真真实实感受到了《共产党宣言》的力量，好些同学都主动递交了入党申请书。<br />
<br />
　　“我们的节目现在已经有了临时党支部，一起排练、一起领悟，就是我们的党组织生活。”英语系、俄语系、德语系……因为这个节目，各个学院的学生党员走到了一起。大学生党员王珏对组织生活也印象深刻，“我们还互相交流学习的感受，经常到渣滓洞白公馆去现场感受革命先辈的精神，对我们日常的学习有很大帮助。”<br />
<br />
　　铁打的营盘流水的兵，8种语言的背后是一个庞大的团队，丁欢老师带领的这个团队也经历了7批不同的成员，前后超过600名大学生曾经辛勤练习、动情表演。<br />
<br />
　　从去年9月首次公演到现在，《共产党宣言》已经成功演出30多场，成为唱读讲传中的知名节目，大家都从这个节目中认识并喜欢上了川外这个明星团队。<br />
<br />
　　“这已经成为我们川外一个特殊的课堂，很多同学都希望加入这个团队。”四川外语学院党委书记马新发说。<br />
<br />
本报记者 戴宇 实习生 林杰<br />
<br />
