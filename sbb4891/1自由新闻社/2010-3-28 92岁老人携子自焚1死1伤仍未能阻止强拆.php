													<h2>92岁老人携子自焚1死1伤仍未能阻止强拆</h2>
						
						
																											<div id="postmessage_535884" class="t_msgfont">2010年03月28日01:24新京报<br />
本报讯 （记者涂重航）昨日上午，江苏连云港市东海县黄川镇一户村民为阻拦镇政府强拆自家的养猪场，二人浇汽油自焚，68岁的男子陶会西死亡，其92岁的父亲陶兴尧被烧伤。目击者称，两人自焚后，拆迁工作人员并未施救，拆迁工作也未停止。<br />
<br />
家属称死者遗体失踪<br />
<br />
据了解，陶会西一家1995年到东海县桃李工业园建养猪场，他们投资20多万，在2亩地上建养猪场，并将家安在此处。去年，东海县在建的310国道准备从桃李工业园通过，需拆除陶会西家以及周围的4户人家。此前，其他4家已拆迁完毕。<br />
<br />
昨晚，死者陶会西的儿子陶秋渔说，7时40分，黄川镇政府拆迁工作组在镇长朱文军（音）的带领下，镇上多个部门近百人来到陶家。陶会西和陶兴尧反锁房门，并向门口倾倒汽油。陶秋渔说，上午8时许，拆迁人员继续砸门，推土机也强行推进。随后，房内起火，两人全身烧着。<br />
<br />
昨晚，陶秋渔说，92岁的爷爷脖子以上，左半边身子烧伤，目前在连云港市第一人民医院抢救。父亲的遗体没有在殡仪馆，不知在何处。<br />
<br />
死者停放2个小时未救<br />
<br />
据目击者称，拆迁时，陶家周围被戒严，他从远处看到陶家冒很高的火头。陶会西的尸体在旁边停放2个多小时，拆迁继续进行。直到猪场被夷为平地，尸体才被拉走。<br />
<br />
“9点多的时候，有人看到我爸在被子里动。”昨日下午，陶会西的儿子说，两人自焚后，没有人进行抢救。据陶秋渔称，其父半年前与镇政府交恶，这次自焚，他们故意未救。<br />
<br />
陶秋渔说，东海县黄川镇政府半年前通知他家，310国道准备经过他家猪场，要求他们拆迁，并没有补偿。当时其父陶会西强烈抵抗，“抱着必死的决心”，当时拆迁未果。<br />
<br />
随后，陶家上访得到答复并非是违章建筑。随后，黄川镇出具评估报告，占地2亩的猪场补偿7.5万元。陶会西对这个补偿不满。“仅地面建筑价值15万元以上。”<br />
<br />
陶秋渔说，镇政府几天前说，他家的猪场不在法律保护范围内，“政府赔多少就是多少”，并说，要赶在4月1日新的拆迁条例颁布前强行拆除他家，否则就拆不了了。<br />
<br />
在协商未果的情况下，前日陶家听闻镇政府准备来强拆，陶会西准备好汽油，并连同92岁的父亲陶兴尧各抱着液化气瓶拍摄录像留念，称要“以死相拼”。<br />
<br />
东海官方证实自焚事件<br />
<br />
昨晚，东海县县委书记郑平未接电话。随后他要求县委宣传部周姓副部长联系本报记者。周某证实，当天黄川镇发生拆迁自焚事件，并称随后会有消息发布，东海县政府对此事将表态。但截至当晚零时，东海未有消息公布。<br />
<br />
据当时在现场的黄川镇派出所所长郑阳说，发生自焚事件后，东海县公安局局长、黄川镇长方赶到现场处理，而自焚事件发生时，镇长朱文军并不在现场，“现场的都是拆迁人员”。</div>

							
							
							
							
