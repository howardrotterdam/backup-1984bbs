为什么会怀上四胞胎，是不是与平时的饮食有关呢？面对记者的疑问，李丹丹笑出了声。她说这可能与丈夫王晓贺的爷爷就是双胞胎有关。“人家都说生多胞胎是有遗传的可能，我觉得可能是这个原因。”李丹丹说，饮食上她其实很随意，从小到大她都不像其他女生一样爱吃甜食，最爱吃的就是国光苹果，而且饮料是一口也不喝。“我怀孕期间就爱吃西红柿，一天能吃上好几个，后来人家说多吃西红柿孩子白，我俩挺黑的，我就希望孩子能白点……呵呵……”<br />
<br />
抚摸着妻子那双肿胀的脚，王晓贺心疼地皱了下眉头。“她人好，皮实，我们家条件不好，吃饱了就行，她喜欢吃啥或者是想吃啥从来都不说。”王晓贺说，妻子整个孕期吃得最多的就是土豆丝和酸菜，吃上最好的饭菜就属鸡蛋炒饭了。丹丹的母亲家养了几只母鸡，自从她怀孕后，鸡下的蛋，父母就像宝贝一样一个一个保管好，都留给她吃。提到这里，李丹丹有些不得劲了，因为父母常年在地里干活，尤其是母亲，只有52岁却因长年劳累看上去很老、很憔悴。<br />
<br />
而来到国健妇产科医院后的这些天，她觉得吃得太好了。“她在家总爱饿，一天吃上六七顿饭还饿，可能是也没有什么营养。”王晓贺说，来到医院后，医院专门为她安排了饮食，一天六顿营养餐。“有猪爪汤、鸡汤、肉，什么好吃的都有，比俺们家过年吃得都好多了……”<br />
<br />
“今年春节前后，我和丹丹去距离我们3里地远的一个人家要账，人家欠我们20块钱，我俩走啊，走啊，还有一个朋友跟着一起去的，当时我就听见‘咕咚’一声，我一看，丹丹摔倒了，我那心里可不是滋味了，你说为了要20块钱，走3里地，结果人家没在家，钱还没要回来。”王晓贺说，生活对于他们一家人来说完全可以用“贫困”代替，然而几日来发生的一切，让一家人都恍如做梦一样<br />
