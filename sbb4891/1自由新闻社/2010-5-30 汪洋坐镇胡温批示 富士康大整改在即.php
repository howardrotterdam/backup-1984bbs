2006-08-31 | 从富士康事件看新闻自由 <br />
<br />
富士康事件搞得沸沸扬扬，作为一个新闻人，第一反应很自然地是站在媒体与记者一边。这其中的原因很简单，本能地从这件事想到了新闻自由。&nbsp;&nbsp;<br />
<br />
是啊，新闻自由，这个东西在我们的土地上是奇缺的，所以大家无不对它珍视有加。以至于一旦碰触到它，就会条件反射式的跳起来。 <br />
<br />
根据朋友提供的资料，第一财经的记者王佑是这样描述她采访的过程的：“当时，本人在《西祠胡同》网站上看到有数十篇一些富士康员工对公司内部人才体制的抱怨。后来，又在浏览一个小型的科技网页的时候，看到了数个富士康员工的 QQ 号码，并与一位刚刚毕业的大学生进行了30多页的聊天，我在《西祠胡同》看到的一些留言，向他一一求证，他也给予了很多建议。文章写完后，我还将该文章交给他确认，他也提了一些意见。在文章正式刊出前，我还直接向富士康确认其中的核心事实。我拨通了公司外联部的贺小姐。根据她的口述（有录音），我将很多关键的说法都一一记录下来，并且进一步修改了该文章。”由此看来，王佑极有可能只是在QQ上与某人进行了一番“聊天”后就写出这篇报道。&nbsp;&nbsp;<br />
<br />
在QQ上采访并无不可，只要是确认了被采访人的身份之后。但是，从这段描述中，我们却很不幸的发现，这篇报道的所有数据，基本上只是从一个QQ上的所谓员工的谈话中得来的。这让我有种恨铁不成钢的愤怒，当时我说了一句：如果她是我的记者，我一定骂死她。是啊，我做了十几年编辑，每次在记者采访前都要提醒一句：你至少要采访到几个点几个人，双方都要采访到！老生常谈了。这涉及一个最起码的“新闻公平”与新闻客观性。在这一点上，我无话可说。 <br />
<br />
那么，是不是因为这点原因，媒体的发言就应该受到指责？我们就没有了质疑的资格了？当然不是。这是我们的权利，因为这的确事关新闻自由。这里我说句题外话，做为媒体工作者，要不得情绪化的东西，而是应该就事论事。无疑，我们谁也无权干涉司法的独立与公正，新闻媒体也不例外，但我们当然可以对司法的程序表示质疑。此次事件中，法院冻结个人财产的行为是否合乎程序就很值得讨论。更别说它冻结的同时，也冻结了另一位无辜者，也就是当事人妻子的财产。另外，法院在处理诉讼时，有它的裁量，而是什么让法院采取了如此果断而极端的措施？这中间又有什么问题，我们当然有资格讨论与追究。更何况，并不能因为记者的错误就排除了类似富士康这种超时加班情况的不存在。而且，富士康的极端做法，显然带有明显的杀鸡儆猴的意味，如果富士康胜诉，这对中国的新闻自由必是一次致命的打击。自然，这其中王佑记者的责任也不可推卸。但，富士康的这种做法我们不能评论吗？对这种制度设计我们不能提出疑问吗？&nbsp;&nbsp;<br />
<br />
其实，大家看看西方的媒体就应该知道，法院在审理案件时，是不能不让媒体对其审理案件进行报道的。这种报道与司法独立与公正没有关系，却能起到监督程序公正的作用。这一点必不可少，所以说，又有什么资格来指责媒体呢？除非你想压制与你不同的看法。&nbsp;&nbsp;<br />
<br />
新闻自由，在中国是件奢侈品，所以许多人并不了解新闻自由的真正意义与内涵。这就使得一旦某事件涉及到了关于新闻自由的争论，就会花样百出。我曾想一个问题：如果这件事发生在美国，会发生什么情况？我想当然地搬出了美国的宪法第一修正案与其后的一系列案例。有朋友说，你搬这些有什么用？我们是在中国。但是我却认为，新闻与新闻自由以及言论自由的概念全来自与西方，为什么我们不能拿西方的作法来说呢？所以我们就说一说。 <br />
<br />
在美国，有关于新闻自由的“第四权”理论，也就是新闻媒体作为一种政府的行政权、立法权与司法权之外的第四种权力，以监督政府。这一理论强调了新闻在现代民主社会中的角色与地位。其实，这已经是常识。大家都知道，既然在一个民主的制度下，如果新闻没有自由，这民主也是极不可靠的。 <br />
<br />
由此，美国等西方国家对新闻自由的权利明确为几类：defensive rights，speech rights，offensive rights。这其中，在speech rights中，明确了新闻媒体即使有事实传达的错误，也不受诽谤罪的追究特权。这其中的的道理不言而喻，由于新闻媒体在快速变化的社会中承担了传达信息的重要角色，如果要求报道与客观事实完全相符，媒体会因为查证事实而成本过高，并且诽谤罪会造成新闻媒体业“噤若寒蝉”，这会对新闻监督的作用造成损害。可见，新闻在西方是一个“特权”行业，这与它的重要作用分不开的。&nbsp;&nbsp;<br />
<br />
昨天看了周克成的一篇博客，感触颇多。克成博文的第一段说得有一点点道理，可往下说去，却越来越不知所云。他认为，“即使工人的工资很低、工作时间很长，那富士康就应该遭受谴责吗？不。假如工人是自愿接受当前工资的、自愿加班的，那我们就没什么好谴责富士康的，不是吗？你认为富士康给工人的工资太低了，那你怎么不给工人更高的工资？你认为富士康对工人太苛刻了，那你怎么不对工人好一点？难道工人都是傻瓜，不会到更好的地方去工作？……总之，作为外人的我们，没有权利指责富士康――只要他并不是强迫工人工作的。……可以理解，媒体几乎一边倒站在第一财经这边，除了轻信第一财经、对第一财经缺乏足够的警惕，还因为他们认为工人工作时间太长、工资太低是不好的，而这些不好的东西，即使有所夸大那也无所谓――甚至，他们会认为，这是应该夸大的，在他们眼中，工人是傻瓜，而资本家有的只是一副丑恶的嘴脸，只会剥夺工人，所以需要他们去体现自己的‘良心’、‘人文关怀’。”&nbsp;&nbsp;<br />
<br />
周同学与他的所有同道一样，他们所有的立论都是基于一个完全竞争的市场。这本无可厚非。可问题就是出在这个假设上。要知道，他们所说的所有一切都是常识，而这些常识不仅是立于假设之上，而且所有依据也是他们闭门拍脑袋拍出来的。如果这样，全世界完全只需要他们几个用那些他们认为放之四海皆准的道理来指导就可以了，还要什么政府、议会、学者？还要搞什么研究？还要什么监督？因为一切监督在他们看来都是“左派”（他们很喜欢用这个词，最大的可能是他们根本不明白这左派到底是什么，更不明白自由主义是什么。他们理解的自由主义大约与毛同学当时反对自由主义时描绘的概念差不多。总之，逆我者皆左派也，不同我者皆左派也，既是左派，必然人品有问题也，可称为一票货色云云。）的，一切监督都是对市场有损害的，或是哗众取宠别有用心的。讲好听些，他们是书呆子，脑子一根筋，有些像一个从未见过女人的小和尚，第一次见到了女人，一个又老又丑的女人，于是偏执地认为天下所有的女人必然都是同一付样子的。讲得不好听，他们有意无意地正在充当帮凶。&nbsp;&nbsp;<br />
<br />
就拿周克成们口口声声讲的“即使工人的工资很低、工作时间很长，那富士康就应该遭受谴责吗？不。假如工人是自愿接受当前工资的、自愿加班的，那我们就没什么好谴责富士康的”这句话来说吧。先不说这种说法是否属实，也不说周克成们是否真的知道真实情况。我们知道，在完全竞争的市场中，工资是劳动力市场的均衡的结果。工资大约正好等于劳动力的边际产值。因此市场会自发开成平均工资率。但是，在一个不完全竞争的市场中，由于工会可能对劳动供给的垄断，或者企业对劳动购买的垄断，工资就会高于或低于劳动力的边际产值。所以，关于上面那句话究竟有没有意义，就集中在到底是不是一个完全竞争的市场上了。可是，就中国目前的情况来看，又有谁敢说中国是一个完全竞争的市场？！首先，中国并没有真正意义上的工会组织，因此，工会对劳动供给的垄断是不存在的。剩下的，就只有企业对劳动购买的垄断了，也就说明目前中国人的工资是低于劳动力的边际产值的。实际情况也确实如此。就以富士康所在的的广东省为例，在过去的12年中，珠三角地区农民工的月工资增幅只有区区68元人民币！再算进物价上涨的因素，实质上是在下降。&nbsp;&nbsp;<br />
<br />
再说说一些朋友曾经调查到的情况，在珠三角地区，许多企业，对工人的管理是十分严苛的。举个我亲眼见过的例子，九十年代在惠州时，大多数工厂的大门上锁的，墙上有铁丝网，宿舍与厂房在一起，除非有负责人比如工头的出门条，工厂大门对工人来说是绝对出不去的。我曾从一位湖北籍的工人口中了解到，他们加班是常态，一般每天工作14到16个小时。对于这点他们愿意，因为可以赚多些钱，比在家乡挣得多。按他们话说，忍几年就回家了。工厂对工人的管理是军事化的，体罚很常见，每天上厕所的次数是有限制的，每次多长时间也有限制，超出了就要受罚，扣钱和体罚。一般来说，工厂会压他们一到两个月工资，如果辞职，这笔钱就没有了。有次老彭和我聊起广东一些工厂的情况时说，他了解到这些工厂的男工人如果喜欢某位女工，最好的礼物就是厕所票。因为女孩子总是比男人上厕所更勤些，每天发的厕所票根本不够用。我们可以说他们是自愿的，可实际这却是制度逼迫的。在这种情况下，我们是应该只是谈经济常识，还是应该更多地去关注，并且对这种制度提出质疑呢？可那些整天高喊口号的人呢，他们有想过制度逼迫吗？他们有想过要对这种制度进行反思吗？又有没有想过，在这种制度下，资本追求利益最大化的行为完全不受任何制约，这与一个政党完全不受制约所产生的情况又有什么不同呢？这种时候媒体的监督是可笑的吗？&nbsp;&nbsp;<br />
<br />
可这帮所谓搞经济学研究的人，却完全忽略这些情况，只想到工人自愿资本最善，从来不考虑其背后的制度因素，也不考虑在这种制度形成的畸形市场上硬套市场理论是多么可怕。却只在脑袋中虚构出一套乌托邦的幻像来，是多么的无知。我们只能猜测，可能会有两种情况让他们这样去想。一种是学了些经济学的条条框框却不消化，更没有一点点哲学认知上的修为。另一种就是因为他们祖师爷张老疯子的世界工厂理论让他们昏了头，为了维护这个理论，只能罔顾事实了。&nbsp;&nbsp;<br />
<br />
