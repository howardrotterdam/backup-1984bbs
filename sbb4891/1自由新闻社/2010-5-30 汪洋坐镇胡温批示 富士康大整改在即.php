<br />
星岛日报<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 富士康集团自杀悲剧一再上演，继26日晚发生员工「第12跳」身亡，27日凌晨再有员工割脉自杀未遂。事件震惊中央，中央调查组已抵达深圳，彻查事件。广东省委书记汪洋27日亦现身富士康园区，富士康总裁郭台铭再搭私人飞机由台湾飞往深圳坐镇。<br />
<br />
　　27日清晨富士康龙华厂区又有员工自杀未遂，这是富士康今年来发生的第13宗员工自杀事件，共造成十死三伤。<br />
<br />
　　富士康连串员工自杀事件震惊中央，中国国家主席胡锦涛和总理温家宝都表关注并作出批示。中央部委联合调查组27日紧急启程前往深圳调查事件，成员由人力资源和社会保障部、全国总工会、公安部组成，人力资源和社会保障部部长尹蔚民亲自带队。中央调查组将至少在深圳调查一周，务求查个水落石出。据悉广东省委书记汪洋当天亦现身富士康园区，与管理层和员工沟通。<br />
<br />
　　郭台铭不得不再杀「回马枪」，在18小时内二度搭专机飞往深圳富士康t解事件。《南方都市报》报道，当时有消息指郭台铭回深后将到医院看望伤者，众多媒体聚集医院门口直到27日18时许守候未果方散。鸿海内部转述郭台铭谈话表示，虽没有把握能立即有效，但保证一定会尽全力处理，请各界给他一点时间，他一定会妥善且有效处理。<br />
<br />
　　据报富士康为激励员工士气，计划上调薪资20%，但鸿海集团否认因坠楼事件调薪，是「因今年经济好转」，并说调薪计划仍在研究中。根据花旗环球证券亚太区下游硬体产业研究部主管张凯伟的预估，鸿海如果调升20%薪资，每季将会增加27亿元新台币的劳工成本、并且吃掉10%至12%的营业获利。<br />
<br />
　　受到员工连跳事件不利影响，在香港上市的富士康股价出现下行压力。今年1月至今，富士康股价已下跌50%左右，市值蒸发458.6亿港元(约 58.87亿美元)。跳楼事件频传也引起富士康代工生产商品的厂家担心企业形象受损，包括苹果、戴尔及惠普电脑等都表示关切，将会深入t解当地工作环境。宏基董事长王振堂27日也首度对事件表达关心，他强调宏与鸿海做生意，不是富士康，因此没有撤单问题。而据美林证券分析，包括专普等世界电脑大厂抽单机会也不大，跳楼事件虽打击鸿海声誉，但预计并不会影响到集团的获利。<br />
<br />
　　台海两岸官方都呼吁媒体，淡化对富士康跳楼事件的报道。据传中国大陆报纸和网站已接到中央和省宣传官员指令，要他们淡化对富士康员工自杀事件的报道，召回赴深圳的记者。大陆门户网站新浪和搜狐用深圳政府的声明和新华社通稿取代了他们自己的报道。<br />
<br />
　　台湾的立法院长王金平也紧急呼吁媒体应共体时艰，不要再渲染此新闻。他28日说，为避免产生对其它公司传染效应，政府应伸出援助之手，富士康也应主动提出需要甚麽样的帮助。<br />
<br />
　　王金平指出，自杀案件因为媒体不断的渲染，确实产生了自杀的传染效应。他希望郭台铭「赶快平息这个事情，做好其它防范的措施，该有的努力就去努力、安抚员工」。王金平也肯定郭台铭在连跳事件的处理，「他最有诚意的是，一听到有问题就马上赶过去，现在心力交瘁，大家共同感受得到。」<br />
<br />
　　不过就有香港团体27日晚号召全球消费者，从6月1日起拒买富士康生产的产品一个月。<br />
<br />
