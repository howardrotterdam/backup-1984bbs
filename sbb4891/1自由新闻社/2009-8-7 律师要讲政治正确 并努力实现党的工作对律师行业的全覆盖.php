																											<div id="postmessage_193734" class="t_msgfont">[编辑] 早年经历<br />
1951年，吴爱英出生在山东昌乐县红河镇清泉村的一个普通农民家庭；在姐妹三人中，她年纪最小，所以有机会多读了几年书。受过一定教育的吴爱英，初中毕业后，就在村里当上了妇女干部。那时，还不到20岁的吴爱英给家乡人的印象是，勤劳、朴实、工作积极；再加上，在那个讲究家庭政治出身的年代，吴爱英自然就成了重点培养对象。<br />
<br />
1971年，吴爱英被推荐作为“工农兵学员”，进入山东大学政治系学习。1973年毕业后，她被分配到中共昌乐县党校任教员。但没过多久，她就被调到昌乐县高崖公社，任党委副书记，并由此进入政界。<br />
<br />
<br />
[编辑] 步步高升<br />
虽然，吴爱英在中共政坛是从最底层的公社党委书记做起的；但是，因为年轻、肯干，她却能够不断地被提升。1975年起，吴爱英调任中共昌乐县委常委、城关公社党委副书记；第二年晋升为中共昌乐县委副书记、县革委会副主任（相当于副县长），兼城关公社党委书记、革委会主任；时年25岁。<br />
<br />
1978年，吴爱英就被调任共青团昌潍地区委员会书记；1982年又晋升为共青团山东省委副书记。由共青团系统被提拔、并在这里工作了十年的吴爱英，在1989年调任山东省妇联副主任；不久又晋升为妇联主任；1993年7月，又被任命为山东省副省长。<br />
<br />
在担任山东省主要领导工作的时候，吴爱英曾多年主管公检法的工作。而在她任内最大的政绩，就是1995年“济南交通警察”的优秀形象受到中央肯定，Jiang Zemin为之题词 ――“严格执法，热情服务”；而“济南交警”的事迹也就获得全国性的推广和学习。所以，在1998年副省长任期结束之际，吴爱英晋升为中共山东省委副书记；并于四年后出任第十届山东省政协主席。<br />
<br />
2003年11月，在山东老家历练三十多年后，吴爱英终于获得机会，走向了人生更大的政治舞台。<br />
<br />
<br />
[编辑] 接掌司法部<br />
吴爱英的教育背景是政治学，而她现在依然是中央党校法学专业在职研究生，正规法律专业教育尚未完成。但是，因为她在山东的工作成绩和雷厉风行、果断严谨的工作作风，2003年11月，吴爱英被宣布出任司法部副部长、党组副书记。这样的安排，显然是为接班而准备的。<br />
<br />
果然，2005年7月，前任部长张福森退休，吴爱英晋升为部长；从而成为史良之后，中华人民共和国第二位女性司法部长。<br />
<br />
<br />
[编辑] 家庭生活<br />
1978年，吴爱英和在昌乐县部队服役的江苏人曹云龙结婚；两人育一女。虽然，吴爱英已经在北京上班，但她的家人依然在济南。</div>

							
							
							
							
