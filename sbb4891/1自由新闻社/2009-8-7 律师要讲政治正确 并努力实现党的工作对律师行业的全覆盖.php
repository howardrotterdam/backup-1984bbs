<br />
截至目前，全国1．4万多家律师事务所中，单独建立党支部3895个，8075家律师事务所联合建立党支部2692个，对2741个无党员的律师事务所，全部派驻了指导员或联络员，提前实现了党的组织和党的工作对律师行业的全覆盖。 <br />
