													<h2>抵制和平演变，九一二美国民众再上街头</h2>
						
						
																											<div id="postmessage_239508" class="t_msgfont">By 鲁克<br />
<a href="http://standardworlddaily.com/blog/archives/1591" target="_blank">http://standardworlddaily.com/blog/archives/1591</a><br />
<br />
<img src="http://johnbatchelorshow.com/schedules/images/29374554.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
奥巴马竞选时说:“We are 5 days away from fundamentally transforming the United States of America , 也就是“我们还剩五天，就将开始从根本上演（转）变美国”。“还剩五天”，如今已经过去了半年，美国究竟发生了什么样的转变？经济没有复苏，失业率居高不下，大规模的政府举债和开支，债务时钟犹如进入读秒阶段的定时炸弹随时都会爆炸。前总统候选人朱利安尼说，“奥巴马政府对经济已经造成了非常，非常，严重的损害，就在这么短的时间内，他伤害的程度超过任何一届总统。”<br />
<br />
不仅如此，奥巴马进行的革新，使美国民众的自由遭受到前所未有的侵蚀。不久前，奥巴马的联邦通讯委员会（FCC）提出要增加电台的地方性以及扩展所有权的多元化，把早已摒弃的“公平播放原则”置入新的概念向公众兜售。这些所谓的要求，无非是要增加对自由电台的管制。我们知道，广播谈话节目的市场主要在保守派的基层，自由派的广播基本上是没有听众的。对自由广播下手，就是要削弱保守派电台的影响力。在自由派纸媒不断失去市场的情况下，奥巴马政府也看到了来自互联网的保守派异见的威胁，新拟定的网络安全法案（Cyber security Bill S. 773）极端鸭霸，居然要给总统超级权力。在所谓的紧急状况下，奥巴马有权掐断普通民众网路的连线。<br />
<br />
奥巴马政府宣称凡登陆政府网站（car.gov）的汽车电脑等于进入了政府领地，政府有权对其控制。奥巴马政府还四处设立耳目，他们用 Flag.gov电邮地址来收集举报的可疑信息。前几日，奥巴马在演讲说，准备建立一支象国防军那样强大的市民国安部队。哇哦，到底是什么样的敌人需要如此强大的武装，难道警察，国安部门还不够用？<br />
<br />
从奥巴马上任开始，他的庞大计划就源源不断。他提出的2010年预算案高达三万六千亿，创下了美国历史新的记录。根据OMB (美国联邦行政管理和预算局)的官员透露，奥巴马政府将十年期预算赤字调至9万亿！<br />
<br />
奥巴马竞选期间承诺给中产阶级减税，事实上，所谓的减税不过是拆东墙补西墙。他推销的《碳限额交易计划》（后来称作魏克斯曼法案）逼迫企业购买碳排放许可，通过这种许可证发售，政府每年将从企业那里盘剥两千四百亿美元（八年两亿）。根据传统基金会（Heritage Foundation）的计算，这个气候变化法案，将造成电费上涨90%，油价上涨58%，天然气价上涨55%，平均下来，让每户家庭一年增加1241元的能源开支，这难道不是加税？<br />
<br />
4603e_1814396为了推行国（公）营医改，他贫富通吃，所谓税富勒贫。加图学者Michael D. Tanner说，如果把所有医改法案引入的新税累加，相当于给纳税人增加五千多亿的税负。其中增加五十亿的烟草税，六百二十亿的酒税，五百二十亿饮料税。对百万收入的富人所得税增加5.4%，把年收入超过二十万美元的中产阶级税率大副调高，限制部分可扣除项目（慈善捐赠抵税）的金额。国税局（IRS）简直成了奥巴马税富勒贫的武器。<br />
<br />
奥巴马政府为了揽权甚至绕过国会，任命亲信作为阁员。他任命的大臣“Czar“已经将近四十个。其权力覆盖面，大的管到国际问题，例如：阿富汗 Czar，中东地区（Central Region Czar） Czar，中东和平Czar；小的介入企业重组和内部管理，比如薪酬Czar，汽车Czar。<br />
<br />
民主党元老级的参议员罗伯特&#8226;博特（Robert Byrd）对 Czars 泛滥提出了警告。他说：“白宫快速而随意的任命阁员方式揽权将会威胁宪法的分权制衡体制”。他说：“最糟糕的是，这些阁员（Czars）管辖的范围已经超越了法典的限制，因为一些项目的辖权只有参议院确认的阁员才具备的。”<br />
<br />
Czars是一种新型内阁，不是一般意义上的助理官员或者国策顾问，是特殊的阁员，是奥巴马的专权大臣。他们的任命一般不用参议院来确认。他们的工作记录及程序不对国会公开，他们可以使用政府的开支却不受国会的监管。比如，奥巴马的薪酬CZAR，其权力大得像一个超级高管。他作为企业之外的官僚却可以直接插手企业管理，而且其辖权范围可以跨行业，跨企业，据统计受他掌控的高管职位多达一百多个。政府用纳税人的钱救助企业，本来就有涉及违宪，现在政府任命一个阁员（Czars）来介入企业内部管理，岂不是错上加错？难道国父设立的三权分立及分权制衡，是奥巴马改革的绊脚石？<br />
<br />
更可怕的是，奥巴马信赖的Czars多数都是极端分子。几天前，绿色工作（CZAR）范&#8226;琼斯终于在舆论压力下，引咎辞职。这位自称是共产党的阁员（Czars），不仅公开发表种族主义的言论，而且参加了为九一一恐怖袭击欢呼的游行。范&#8226;琼斯认为九一一是美国政府的自导自演，他甚至在所谓追求九一一真相的联名信上签名。范&#8226;琼斯仇恨剥削的资本主义制度，他说不仅要搞绿色工程，还要彻底更新美国整个制度。这样一个反美的极端分子居然进入了奥巴马的内阁！<br />
<br />
美国民众选奥巴马当总统，并没有选范&#8226;琼斯来代理美国绿色工作，更没有授权让他来彻底“更新整个制度”，但奥巴马相信范&#8226;琼斯之流的建议，对美国民众的抗议和质疑佯装不知。更有甚者，上半年的倾茶党运动大规模的示威抗议，奥巴马却居然说他不知道。是不知道，还是视而不见，总统当选了就不再用理会管民众的诉求和抗议？<br />
<br />
同样，民主党多数的国会如今是民意支持率最低的国会。国会议员是民选的不错，但是民众希望自己的代言人至少要对法案负起责任。但是事实上，上千页的法案，一些议员看都没看就投票，甚至一位民主党议员说，看法案，说的轻巧，没有时间怎么看。大总统奥巴马也是如此，他有时间向全国的小学生广播教育他们多洗手，却没有时间阅读自己推销的法案。自己不看也就算了，却催促国会的议员赶紧投票，赶紧通过这些法案。<br />
<br />
奥巴马过去说什么，不再论大政府小政府，民众要一个负责任的政府。难道这就是他承诺负责任的政府？民众还期待什么奥巴马新政，这根本就是滥政。这种政策有什么革新之处吗？这不就是走东欧，西欧都厌弃的福利社会主义的道路嘛。福利主义，社会主义不是美国人要走的方向，这和国父建国的宗旨完全背道而驰。<br />
<br />
