																											<div id="postmessage_79655" class="t_msgfont">才仅仅10年之前，白领还是一个全社会人人称羡的身份。万科地产甚至将其出版的系列图书命名为《白领》。白领是指那种在高级写字楼里上班的专业技术人员，特点是高学历、高收入。特别是写字楼里外资企业，更是白领群体云集的根据地。<br />
<br />
白领意味着体面的工作、优雅的修养、丰富的精神体验。从某种意义上讲，白领简直成为时尚的代名词。<br />
<br />
白领必定毕业于名牌大学，甚至是硕士、博士或海归，每天朝九晚五打卡，坐在格子间的电脑旁，MSN，麦当劳，卡布奇诺，网恋，丁克，地铁，打的，坐经济舱，住星级宾馆，泡吧，煲电话，听蓝调，加班，夜生活，圣诞节，一夜情，斯诺克，暂住证，红酒，抽555，住租来或按揭的公寓，买简约的宜家家具，收藏CD，谈论《老友记》，向往喝西藏，留恋于丽江，铁杆驴友，不看中文报纸不看中国电影，看《国家地理》《名牌》《读书》杂志，看卡夫卡看张爱玲看伊朗电影，洁癖，乡愁，健身，瑜伽，养吉娃娃，香水衣服鞋子泡吧旅游鲜花买书买CD看电影，月光一族。<br />
<br />
白领的产生是中国市场经济发展初级阶段末期的典型现象，证明了&quot;知识改变命运&quot;。白领大多只出现在一线城市。面对WTO的前夜，这些有文化有知识的年轻人开始尝试一种西方发达国家中产阶级的雅皮士生活。绅士与淑女，是充满这些新思想的青年人的人生目标。《了不起的盖茨比》和《傲慢与偏见》是他们的必读书。爱情、教养、文化、艺术、体验、精神贵族深深地吸引着他们。<br />
<br />
10年过去，物是人非。回头看看，当年怀着白领梦&quot;范进中举&quot;，当许多大学生兴冲冲踏出大学这个高级职业培训监狱大门的时候，却必须接受与黧黑的农民父亲同场竞聘的残酷现实。<br />
<br />
曾经的白领已经老去，在一场百年不遇的经济危机面前，破产的破产，失业的失业，离婚的离婚。当孕育白领的贸易、广告、房地产、IT和制造业风吹雨打流水落花，脆弱的白领蓦然发现，曾经雪白挺括的领口，已经被冰冷的汗水洇得皱皱巴巴一片姜黄。<br />
<br />
春天来的时候，老去的白领继续徘徊于物价和房价飞涨的城市。伫立在林立的写字楼脚下，他今天会收到一个面试通知么……白领的传说就这样陨落了。<br />
<br />
与此同时，一个充满神秘色彩的社会群体已经夺去了全中国所有的光芒，他们开着&quot;自己的&quot;大排量名牌汽车，出入高档酒楼，高级夜总会，乘坐头等舱或软卧，住星级宾馆，拥有黄金位置的几处豪宅，购全套红木家具，在位置最好、景观最佳，装修最豪华、质量最安全的办公楼上班，独立办公室，不打卡，饭局，会面，喝茅台五粮液，品天价普洱，抽极品中华，精装《毛评二十四史》，VIP，炒股投资保险理财，收古玩字画珠宝黄金，高级会所，劳力士，路易威登，奢侈品，国际顶级品牌服饰，高尔夫，公派出国，移民，护照，拉斯维加斯，美容减肥按摩，组织体检，疗养，免费医疗，贵族学校，MBO，脱产学习，党校，佣人，情人，养藏獒，带薪假……<br />
