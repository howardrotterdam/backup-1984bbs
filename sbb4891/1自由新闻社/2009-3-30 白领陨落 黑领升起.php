																											<div id="postmessage_82380" class="t_msgfont">投资环境恶化导致股市回暖(2009-04-17 12:46:00)标签：杂谈&nbsp; &nbsp; <br />
<br />
　　透过我的极视官网搜集了很多网友的反<br />
　　透过我的极视官网搜集了很多网友的反馈，我讲的他们基本都听懂了，不过认为我说的都是宏观层面的东西，建议讲一些实际的，教他们应该具体怎么做，很多人也给我写信，当然基本上都是在股市被套牢的。我想说的是，我自己是金融博士、金融专家，但我的最高战略指导思想是绝对不碰金融资产，而且是越复杂的越不碰，越要签合同的越不碰，合同越厚的越不碰，只是在房地产与现金之间来回折腾，这就是我学了一辈子的结论。最近中国股市在大涨，很多人的心情很激动，就在想这是不是一个经济复苏的前兆。<br />
<br />
教他们应该具体怎么做，很多人也给我写信，<br />
 <br />
<br />
　 　　透过我的极视官网搜集了很多网友的反股市回暖的实质意义<br />
<br />
 <br />
<br />
　　二月份中国股市最高涨到了2400多点，请股民注意为什么一二月份会上涨？带着这个疑问想一下2006年，你会发现股市的基本面是不变的。中国2006年开始的股市泡沫、楼市泡沫和通货膨胀，如果按照日本上世纪80、90年代的故事，我们肯定认为是流动性过剩，因为当时的日本银行降低利率水平、放松信贷规模，使得银行信贷大量的流通，造成流动性过剩。2006年学术界就发生一个争论，大部分经济学家说中国也是流动性过剩，当时我认为并不是简单的流动性过剩，而是由于企业所面临的投资营商环境开始恶化。政府的各项政策包括宏观调控像提高利率跟存款准备金率，进一步打击了我国企业的投资营商环境，因此大量的资金从制造业转到股市楼市造成泡沫现象。同样的问题到了2009年，投资环境在没有有效改善之外，加上国际金融海啸的冲击，使得企业所面临的环境更加恶劣。如果这个时候银行体系大量放松贷款，那企业能做什么？<br />
<br />
当然基本上都是在股市被套牢的。我想说的是<br />
 <br />
<br />
　　以一月份为例，一月新增贷款1.62万亿元，名义上听起来是一个很好的数字，银行为了刺激经济，增加了1.62万亿元的信贷。1.62万亿当中有0.6万亿左右是企业的票据融资，剩下的1万亿大部分是基础建设，真正用于改善投资营商环境或者说真正用于民营企业或者讲的更具体一点用于中小企业的几乎是零，也就是说这么大的信贷规模推出来的结果一个是银行给地方政府从事基础建设，一个是银行给大型企业尤其是大型国企进行票据融资。而且票据融资的利息非常低，目前是1.5%。票据融资只能做短期周转之用，当成企业的流动资金，比如发薪水、购买原材料等。去年十二月，票据融资是5000亿，一月是6239多亿，那么当去年12月拿到了5000亿，今年一月又拿到6239亿的钱之后，由于企业面临着投资营商环境的恶化，他准备做什么，又能干什么？因为票据只能用在短期，所以不能炒楼，只能炒股。当时我在极视网回答网友问题的时候说一万多亿的资金部分会进入股市拉抬股价，而这也就是近期股市上涨的原因。<br />
<br />
 <br />
<br />
当然基本上都是在股市被套牢的。我想说的是<br />
　　中国银监会在今年2月13号通知要查帐，股价立刻大跌，因为要赶快把钱套现放回银行。激增的流动性资金去向也引起了监管层的注意，但是和07年不一样，07年银监会调查违规资金入市的力度非常大，这一次银监会没有向各大银行严查是否有违规资金入市的情况，央行也只是提醒各大银行关注票据激增的现象，没有警告也没有查处，结果是雷声大雨点小，所以这一波的走势完全印证了我在2006年的观点。过去很多专家学者总认为中国的股市是乱七八糟不符合基本面的，中国的股市不是不符合基本面，而是他们没有搞清楚什么是基本面。依我来看，我们的股市非常符合基本面，而且符合的不得了，而股市的基本面就是我国投资营商环境迅速恶化，这个基本面太重要了，它决定了我国这么长时间的股市走向。同样的问题到了2009年，投资营商环境更加恶化的情况下，银行增加了大规模的信贷给企业，而企业在投资营商环境恶化之下找不到投资出路，而钱只能用在短期，他们能干的只是炒股，从而拉抬股市造成了股市回暖现象，但是这种回暖现象所象征的实质意义就是投资营商环境的持续恶化。<br />
<br />
 <br />
<br />
 <br />
<br />
当然基本上都是在股市被套牢的。我想说的是<br />
 <br />
<br />
　　政府为了提升经济推出了很多的经济刺激方案，因此我国目前企业的资金还是比较充裕的，因为政府特意的、大量的释放资金入场。08年由于政府宏观调控的误判，银行透过提高利率和存款准备金率大量回收资金，使得所有企业缺资金，大企业缺大钱小企业缺小钱，在企业缺钱的情况下，投资营商环境又恶化，所以股市是连续下跌，但是今天投资营商环境更加恶化了，可是钱突然多了，其结果必定反映在股票市场，并且是股价大涨，可是这个能否持续就要看银行资金能否持续，只要银行资金继续宽松股价就会涨，而这个时候股民要小心了，这个叫泡沫现象，这个泡沫和06年、07年、08年完全不一样。为了给广大股民一个正确的指导方针，我把06、07、08、09这四年股市走势的资金驱动，也就是资金与环境恶化两个互动你可以非常清楚地判断出股市的走向，我希望09年能让各位股民赚回一点钱。<br />
<br />
 <br />
<br />
当然基本上都是在股市被套牢的。我想说的是<br />
　　06年开始一直到09年整个社会的投资营商环境直线下滑，06年和07年进入股市拉抬股价的资金，就是那些制造业的资金，而且我认为是以民营企业为主的制造业资金进入股市、进入楼市。由于环境恶化，所以很多民营企业家不想干了，大量资金进入楼市与股市的结果就是从06年开始到07年年底的泡沫现象，到了08年投资营商环境持续恶化，可是政府的宏观调控从07年政府的中央经济工作会议之后，执行严格的两防政策，银行大量收回授信，所以使得大中小型企业各个缺钱，再加上投资营商环境的恶化，使得整个股市大幅走低，一直到了08年的10月，政府推出了4万亿的救市工程，这个概念推出来之后激活了股票市场的资金流入，所以形成暂时股市上升现象，但是其后还是会下跌，因这都是短期泡沫， 12月5000亿，一月的6000多亿以及后来的资金持续注入企业，而企业所面临的投资营商环境持续恶化，所以这一次的驱动力量就是大型企业，尤其是大型国企，他拿到了银行授信，部分进入股市，造成泡沫现象。那我们如何判断09年往后的股市呢？按我现在来看，投资营商环境恶化是个必然，所以大家不要期待企业会拿钱去好好的干实业，他们手上只要钱多了就开始炒楼炒股，手中有钱又不好投资又碰到十大产业振兴方案，所以这一拉抬起来很过隐。<br />
<br />
