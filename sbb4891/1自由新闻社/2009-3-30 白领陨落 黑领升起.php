教他们应该具体怎么做，很多人也给我写信，<br />
　　货币市场上的投资机会<br />
<br />
当然基本上都是在股市被套牢的。我想说的是<br />
 <br />
<br />
　　除了股票市场之外我建议各位，由于美国政府需要投放大量的美元进入市场，包括以前我说的，要投资超过十万亿美元进行美国的救市，而且都知道货币印的越多越贬值，但是从去年的7月开始一直到3月，我们发现美金不跌反涨。一个政府会让这个国家的货币不跌反涨只有一个原因，那就是他要借钱。如果要借钱的话，美元涨的越高越有利，所以目前我认为美国政府为了达到借钱的目的一定要让美元涨，然而美元涨是违反市场规律的，其结果就会造成外汇市场上各种货币之间价格的大幅波动，而大幅波动就是机会。以目前美元与欧元为例，这个时刻我肯定的判断，这是欧盟与美国之间的勾结。请想一下，在目前情况之下，什么样的汇率是既有利于美国又有利于欧盟？那就是美元涨欧元跌。欧盟的大国只有德法两个，这两个国家是以出口为主要的经济动力，所以欧元跌对于德法两国的出口有利好的刺激作用，能够尽快使得欧盟经济脱离萧条；而美国政府要借钱，所以美元要涨，这也是为什么美国政府的平准基金正在大肆的抛欧元。当你了解这个原则之后，大家可以在美元与欧元之间取得一个平衡。欧元越跌你将来越要进欧元，要低价吃进，将来会反转，具体什么时候我不知道，但是一定会反转。所以今天我认为不论是在本国或在国际市场上都会有大量的机会给老百姓赚钱。在这个时候就要有新概念，要彻底的理解股市、外汇市场。<br />
<br />
