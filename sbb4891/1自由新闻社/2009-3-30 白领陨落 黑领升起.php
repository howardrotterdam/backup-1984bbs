<br />
 <br />
<br />
馈，我讲的他们基本都听懂了，不过认为我说　美国政府操纵黄金<br />
<br />
　　透过我的极视官网搜集了很多网友的反<br />
 <br />
<br />
　　投资者要买黄金要用什么币去买？黄金都是美元定价的，想买黄金一定要用美元去买，因此黄金的操纵者我认为是美国政府，黄金价格大肆拉抬的目的就是透过投资者用美元去买黄金而大量回收美元，这样有利于美元的升值。当然，美国政府不会这么说，这是我对它的判断，所以我认为美国政府一方面通过他的平准基金抛欧元，让欧元跌，美元相对涨，同时为了大量收回货币，除了在本国做一些大家不知道的包括卖国债给银行、与银行直接交易等大量收回美金的事情之外，在国际市场上拉抬黄金价格大量回收美元，使美元大量回收有利于美元升值。<br />
<br />
　　通过前面的分析，指出了中国股市涨、黄金涨、美元涨、欧元跌的原因，所以，如果想投资一定要非常清楚的了解背后的四大原因：中国股市涨是寄托于不断恶化的投资营商环境，并且银行资金大量进入股市炒作有概念的股票，同时炒作外资想进入以及政府想拉抬的行业；黄金会随美元的大涨而大涨，大涨的原因是美国政府透过黄金的涨价收回美元，有助于美元的升值。美元升值是违反经济理论的，仅仅是因为美国政府的操纵，通过平准基金大量的抛欧元，使欧元贬值，有利于欧盟中德法等主要经济体的复苏。<br />
