<br />
人类都知道，对黑领来说，收入绝对不止薪水这一块，医疗交通吃喝拉撒贪污受贿等等，所有的地方都享受纳税人无偿供养，每月的车贴甚至比农民工辛苦一个月的薪水还要多，他们也可以在超市买个床单裤衩都开发票报销，或者把免费领来的大量昂贵药品卖钱。甚至嫖娼也要发票。可以说，所谓黑领，就是除了没给其配备法律意义上的配偶外，其它都是享受无偿供给的。<br />
<br />
黑领阶层之所以生活水平急剧提高，是因为其垄断了包括政治、法律、经济、信息在内的一切社会资源，他们消耗了至少一半以上的中国国民收入。他们的崛起，构成了中国新二元社会的显赫一极。这个群体虽然相对数量少，但是绝对数量庞大。粗略估计一下，这种以寄生垄断为业的黑领在全国约有2000万以上。<br />
<br />
比起10年前苍白的小资白领来，只有这些享受和垄断了政治权利的人才真正的实现了几代中国人的梦想，他们绝对已经达到甚至超过欧美发达国家生活的水准。当然，另外一极的其他&quot;普通老百姓&quot;则是标准的第三世界贫穷国家的国民。<br />
<br />
来自官方背景的黑领对来自民间草根的白领的颠覆，体现了政治权力向自由经济领域的渗透和僭越，以政治权力篡夺经济权力。这种食利自肥的经济身份使官方的超脱精神和公益基础遭到侵犯，合法性受到玷污，政治的伦理尊严荡然无存。官方由民众的仆从变成&quot;民主&quot;DD民众的主子，由公共利益的正义仲裁者演化为自身利益集团的代言人，从国家和社会的守夜人退化为自私卑鄙的盗窃者。这是一种极其危险的倾向。<br />
<br />
白领阶层可以说是开放的，或者说穷人的孩子可以通过读书实现白领梦。正因为如此，白领在大学扩招后人力资源充沛的中国急剧贬值。相对而言，黑领阶层则完全是封闭的，正因为封闭，才会奇货可居炙手可热。公共机构实际上已经成为官僚权力集团把持的私家后院，普通人家的孩子要想进入这个群体，理论上说不是不可能，只能说DD很渺茫。<br />
<br />
不错，公务员是公开招聘的，垄断官方企业的职位也是面向社会招聘的，只要你拥护那个党，你就可以报名考试。但地球人都知道这里面的规矩DD潜规则，考不考得上并不取决于考试分数。<br />
<br />
黑领的特殊之处是已经走向组织化和正在走向世袭化，前者巩固，后者继承。在白领黯然陨落之后，黑领的低调崛起在全社会引发了一轮又一轮的考公务员热。同时，黑领也成为所有商家追逐的目标，他们比白领具有更真实更强悍的消费力。他们走到哪里，哪里就物价飞涨；他们对地产的投资，使农民失去了土地，使白领丧失了家园。<br />
<br />
当白领遇见黑领，立马被压出西装下面的&quot;小&quot;来。今天，一个供职于夹缝状态私企的所谓白领，以他微薄的收入仅够维持温饱而已，消费对他来说已经是一个太过夸张和绝望的词语。不久前官商云集（没有几个身家低于千万）的两会上，一个黑领代表或是同情或是鄙夷地建议小白领们应该去卖肉DD不是出卖自己的肉体，是卖猪肉。<br />
<br />
在这场席卷地球的金融风暴中，无数外企破产倒闭、业绩滑坡，覆巢之下，纷纷裁员降薪，白领们仓皇失业。与此相反，中国官有组织却财大气粗逆市飘红，令世界500强为之羡慕，黑领们仍然可以毫无罪恶感的集体加薪。<br />
<br />
近水楼台先得月，砸向黑领掌心的4万亿投资计划如同一针鸡血，使无数红了眼的黑领们激动得加额称庆DD还是中国好、组织好啊。说实话，贫困潦倒的白领们从这 4万亿民脂民膏中想捡点残羹剩饭也是痴心妄想。所以说，&quot;孔乙己&quot;这样卑微的白领如何能与&quot;假洋鬼子&quot;这样傲慢的黑领同日而语？<br />
<br />
如果说白领曾经掀起一股托福热、小资热的话，黑领的江湖则使传统国学和势利文化大热。易中天的阴谋学、王立群阎崇年的帝王学、于丹的犬儒学和马未都的收藏学等等，无不映照了黑领这个社会核心消费阶层的形成。<br />
<br />
黑领的兴起说明，20年前的那场轰轰烈烈的反腐败反官倒运动遭到残酷镇压后，新兴知识群体在与权力群体博弈中已经完全丧失了主动权。权力经济终于在近10年从量变到质变，完成了对知识经济和自由经济的彻底颠覆。权力组织在文革后重新收复了对共和国的垄断话语权。<br />
<br />
近年来热映银屏的《激情燃烧的岁月》、《军歌嘹亮》、《金婚》和《天下兄弟》等剧，集中反映了文革时期第一代黑领的优裕生活。权力特权下的文革被营造被演绎得无比温馨富足和谐，根本看不到知识阶层生不如死和农民阶层食不果腹的悲惨灾难。这种以主旋律色彩出现的怀旧情绪充满复辟邪恶和美化罪恶的企图。<br />
<br />
曾经的党校高材生、当代厚黑学大师冯仑老板毫不客气地把白领鄙视为&quot;房奴&quot;，一个&quot;奴&quot;字撕下了一群人看似体面的假领。诚然，白领没有任何社会权利，没有罢工权，没有选举权，没有话语权；他们没有权势，没有资本，没有门第。相反，黑领则是这个国家的上帝选民。他们的房子票子车子等等除过老婆之外，都一概享受无偿配给，几乎不用跟&quot;普通老百姓&quot;们争来抢去的所谓市场发生任何关系。<br />
<br />
白领是如此脆弱而不堪一击，一套小小栖身的房子就可以将其压垮；而黑领是如此坚不可催固若金汤，一场导致无数孩子死亡的&quot;三鹿&quot;惨案，也未见一人因职务犯罪被追究法律责任，仅仅纪律处分了事。<br />
<br />
因为对立法权和司法权的把持，黑领群体成为名义上和实质上的共和国公民，他们普遍享受到一个共和国公民所应当享受的一切政治权利。从基本人权、财产权、公民权、选举权和一切社会福利，他们都应有尽有的得到了充分保护和满足。<br />
<br />
与之相反，日渐普遍和经济失宠的白领群体则无法享受到基本人权保证，更遑论公民权和社会福利。他们被官方称之为与&quot;公民&quot;相对立的&quot;普通老百姓&quot;或者&quot;群众&quot;。相对于&quot;共和国公民&quot;而言，&quot;普通老百姓&quot;在政治层面和法律意义上，仅相当于&quot;人畜&quot;、&quot;奴隶&quot;或者&quot;机器人&quot;。他们经常被官方作为十几亿的巨额国家财产来看待，说好听点叫作&quot;劳动力资源&quot;。其对外的称呼为&quot;人民&quot;，多用在&quot;伤害中国人民感情&quot;的时候。<br />
<br />
白领的陨落代表着知识精英的穷途末路和理性精神的落败，黑领的兴盛代表着权力意识形态的扩张，和反知识重权力的血统论和阴谋论王者归来。&quot;知识贬值&quot;必然带来&quot;读书无用论&quot;的盛行，中国社会从此向封建资本主义进一步靠拢。社会文化日渐沙化和盐碱化，重归流氓文化和宫廷权谋黑幕政治的覆辙。<br />
<br />
黑领对白领的阻击和绞杀使构成未来社会主流的新兴中产阶级胎死腹中，建立宪政公民社会的启蒙运动被迫土崩瓦解。这种财阀与权贵的合力扼杀使一个民族的创新能力和创造力严重退化直至丧失。社会结构和信息结构进一步被凝固被肢解，青年一代被年迈保守的既得利益者压制封堵在社会最底层。健康的社会流动和财富循环陷于停滞，推动社会进步的活力和源泉被窒息被堵死。<br />
<br />
胜者为王的狼图腾文化、不择手段的官场权谋文化、暴殄天物的面子文化和崇高伟大的满清皇帝戏之所以大行其道，正映射着白领规则的陨落与黑领规矩的升起，中国社会由知识和文明的艰难复苏，无可挽回地退回到野蛮与无知的权力通吃、弱肉强食中去。<br />
<br />
人往高处走，水往低处流。在全社会的羡慕、嫉妒和仇视之中，黑领阶层一方面继续低调的巩固其社会地位(政治地位和经济地位)，另一方面在完成原始积累后，他们开始悄然向新大陆挺进DD携款外逃，或者投资移民，实现自己正式加入世界发达国家高级人类的梦想，同时也使自己的后代永远彻底的摆脱水深火热的中国。<br />
