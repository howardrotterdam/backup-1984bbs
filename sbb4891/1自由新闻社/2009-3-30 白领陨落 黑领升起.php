他们就是在全中国一线二线三线城市遍地开花，全面崛起的新兴黑领阶层。相对于干干净净清清白白的白领，他们的衣服是黑色的，汽车是黑色的，脸色是黑色的。他们的收入是隐蔽的，生活是隐蔽的，工作是隐蔽的……所谓隐蔽，就是像站在黑夜里的黑衣人，你知道他在，他也知道他在，但你不知道他什么样，在做什么。他们就是就职于政府和官有垄断企业的那个庞大群体。<br />
<br />
10年间，官有建筑已经屡屡刷新了所有中国城市的高度。在气度辉煌富丽堂皇的官方办公楼面前，商业写字楼登时被压出逼仄吝啬的寒酸来。从容积率、配套、装修等各方面，拔地而起的&quot;大裤衩&quot;成为城市黑领新贵们的&quot;鸟巢&quot;。白领和他的OFFICE一起，被黑领的裤衩遮住了所有的阳光。<br />
<br />
10年间，通过土地财政和垄断政治权力，官方组织一步步通过各种手段将社会财富向自己手中集中。不仅以重税和重复收费罚款的方式，从横向上苛刻聚敛社会财富，而且以资源浪费和环境污染等方式，从纵向上大肆透支谋夺子孙后代赖以生存的根基。<br />
<br />
官有经济在垄断的无竞争市场所向披靡，源源不断的暴利如滚滚长江。水气电油电信金融烟草卫生教育海关公路等行业自不用说，即使出版、邮政、新华书店、市政、环卫、公交、盐业、矿业、铁路、民航、文化、体育、新闻、旅游、土地等这些领域，因为禁止自由竞争，其利润之丰厚仍足以使任何外企眼红得流鼻血。<br />
<br />
