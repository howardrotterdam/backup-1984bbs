													<h2>白领陨落 黑领升起</h2>
						
						
																											<div id="postmessage_75539" class="t_msgfont">作者：杜君立<br />
<br />
才仅仅10年之前，白领还是一个全社会人人称羡的身份。万科地产甚至将其出版的系列图书命名为《白领》。白领是指那种在高级写字楼里上班的专业技术人员，特点是高学历、高收入。特别是写字楼里外资企业，更是白领群体云集的根据地。<br />
<br />
白领意味着体面的工作、优雅的修养、丰富的精神体验。从某种意义上讲，白领简直成为时尚的代名词。<br />
<br />
白领必定毕业于名牌大学，甚至是硕士、博士或海归，每天朝九晚五打卡，坐在格子间的电脑旁，MSN，麦当劳，卡布奇诺，网恋，丁克，地铁，打的，坐经济舱，住星级宾馆，泡吧，煲电话，听蓝调，加班，夜生活，圣诞节，一夜情，斯诺克，暂住证，红酒，抽555，住租来或按揭的公寓，买简约的宜家家具，收藏CD，谈论《老友记》，向往西藏，留恋丽江，铁杆驴友，不看中文报纸不看中国电影，看《国家地理》《名牌》《读书》杂志，看卡夫卡看张爱玲看伊朗电影，洁癖，乡愁，健身，瑜伽，养吉娃娃，香水衣服鞋子泡吧旅游鲜花买书买CD看电影，月光一族。<br />
<br />
白领的产生是中国市场经济发展初级阶段末期的典型现象，证明了“知识改变命运”。白领大多只出现在一线城市。面对WTO的前夜，这些有文化有知识的年轻人开始尝试一种西方发达国家中产阶级的雅皮士生活。绅士与淑女，是充满这些新思想的青年人的人生目标。《了不起的盖茨比》和《傲慢与偏见》是他们的必读书。爱情、教养、文化、艺术、体验、精神贵族深深地吸引着他们。<br />
<br />
10年过去，物是人非。回头看看，当年怀着白领梦“范进中举”，当许多大学生兴冲冲踏出大学这个高级职业培训监狱大门的时候，却必须接受与黧黑的农民父亲同场竞聘的残酷现实。<br />
<br />
曾经的白领已经老去，在一场百年不遇的经济危机面前，破产的破产，失业的失业，离婚的离婚。当孕育白领的贸易、广告、房地产、IT和制造业风吹雨打流水落花，脆弱的白领蓦然发现，曾经雪白挺括的领口，已经被冰冷的汗水洇得皱皱巴巴一片姜黄。<br />
<br />
春天来的时候，老去的白领继续徘徊于物价和房价飞涨的城市。伫立在林立的写字楼脚下，他今天会收到一个面试通知么……白领的传说就这样陨落了。<br />
<br />
与此同时，一个充满神秘色彩的社会群体已经夺去了全中国所有的光芒，他们开着“自己的”大排量名牌汽车，出入高档酒楼，高级夜总会，乘坐头等舱或软卧，住星级宾馆，拥有黄金位置的几处豪宅，购全套红木家具，在位置最好、景观最佳，装修最豪华、质量最安全的办公楼上班，独立办公室，不打卡，饭局，会面，喝茅台五粮液，品天价普洱，抽极品中华，精装《毛评二十四史》，VIP，炒股投资保险理财，收藏古玩字画珠宝黄金，高级会所，劳力士，路易威登，奢侈品，国际顶级品牌服饰，高尔夫，公派出国，移民，护照，拉斯维加斯，美容减肥按摩，组织体检，疗养，免费医疗，贵族学校，MBO，脱产学习，党校，佣人，情人，养藏獒，带薪假……<br />
<br />
他们就是在全中国一线二线三线城市遍地开花，无远弗届全面崛起的新兴黑领阶层。相对于干干净净清清白白的白领，他们的衣服是黑色的，汽车是黑色的，脸色是黑色的。他们的收入是隐蔽的，生活是隐蔽的，工作是隐蔽的……所谓隐蔽，就是像站在黑夜里的黑衣人，你知道他在，他也知道他在，但你不知道他什么样，在做什么。他们就是就职于政府和官有垄断企业的那个庞大群体。<br />
<br />
10年间，官有建筑已经屡屡刷新了所有中国城市的高度。在气度辉煌富丽堂皇的官方办公楼面前，商业写字楼登时被压出逼仄吝啬的寒酸来。从容积率、配套、装修等各方面，拔地而起的“大裤衩”成为城市黑领新贵们的“鸟巢”。白领和他的OFFICE一起，被黑领的裤衩遮住了所有的阳光。<br />
<br />
10年间，通过土地财政和垄断政治权力，官方组织一步步通过各种手段将社会财富向自己手中集中。不仅以重税和重复收费罚款的方式，从横向上苛刻聚敛社会财富，而且以资源浪费和环境污染等方式，从纵向上大肆透支谋夺子孙后代赖以生存的根基。<br />
<br />
官有经济在垄断的无竞争市场所向披靡，源源不断的暴利如滚滚长江。水气电油电信金融烟草卫生教育海关公路等行业自不用说，即使出版、邮政、新华书店、市政、环卫、公交、盐业、矿业、铁路、民航、文化、体育、新闻、旅游、土地等这些领域，因为禁止自由竞争，其利润之丰厚仍足以使任何外企眼红得流鼻血。<br />
<br />
在当下中国随便哪一个城市，一个大腹便便的税务监管员都可以开着路虎SUV上班，他的办公室面积有多大、装修得有多豪华不必说，只消告诉你一句，他可以在单位里健身桑拿游泳……一个刚刚工作两年的警察就已经买车买房――没要父母的钱也没按揭……一个国家电网公司的抄表员基本月薪达到8000元……<br />
<br />
简单推算一下，全国有1000多个省级，20000个厅级，好几万到十来万个县级，这还不包括北京的中央部门和军队警察系统。较发达地区普通黑领年收入10到20万元极普遍，年终发个十万元奖金不是什么稀奇事，而这也不仅仅是税务部门才有这个财力。这是“合法”的收入，这一部分财产是不怕公示的。去年就有新闻称，南方某地所有的黑领都有两部车，而且很正常。<br />
<br />
人类都知道，对黑领来说，收入绝对不止薪水这一块，医疗交通吃喝拉撒贪污受贿等等，所有的地方都享受纳税人无偿供养，每月的车贴甚至比农民工辛苦一个月的薪水还要多，他们也可以在超市买个床单裤衩都开发票报销，或者把免费领来的大量昂贵药品卖钱。甚至嫖娼也要发票。可以说，所谓黑领，就是除了没给其配备法律意义上的配偶外，其它都是享受无偿供给的。<br />
<br />
黑领阶层之所以生活水平急剧提高，是因为其垄断了包括政治、法律、经济、信息在内的一切社会资源，他们消耗了至少一半以上的中国国民收入。他们的崛起，构成了中国新二元社会的显赫一极。这个群体虽然相对数量少，但是绝对数量庞大。粗略估计一下，这种以寄生垄断为业的黑领在全国约有2000万以上。<br />
<br />
比起10年前苍白的小资白领来，只有这些享受和垄断了政治权利的人才真正的实现了几代中国人的梦想，他们绝对已经达到甚至超过欧美发达国家生活的水准。当然，另外一极的其他“普通老百姓”则是标准的第三世界贫穷国家的国民。<br />
<br />
来自官方背景的黑领对来自民间草根的白领的颠覆，体现了政治权力向自由经济领域的渗透和僭越，以政治权力篡夺经济权力。这种食利自肥的经济身份使官方的超脱精神和公益基础遭到侵犯，合法性受到玷污，政治的伦理尊严荡然无存。官方由民众的仆从变成“民主”――民众的主子，由公共利益的正义仲裁者演化为自身利益集团的代言人，从国家和社会的守夜人退化为自私卑鄙的盗窃者。这是一种极其危险的倾向。<br />
<br />
白领阶层可以说是开放的，或者说穷人的孩子可以通过读书实现白领梦。正因为如此，白领在大学扩招后人力资源充沛的中国急剧贬值。相对而言，黑领阶层则完全是封闭的，正因为封闭，才会奇货可居炙手可热。公共机构实际上已经成为官僚权力集团把持的私家后院，普通人家的孩子要想进入这个群体，理论上说不是不可能，只能说――很渺茫。<br />
<br />
不错，公务员是公开招聘的，垄断官方企业的职位也是面向社会招聘的，只要你拥护那个党，你就可以报名考试。但地球人都知道这里面的规矩――潜规则，考不考得上并不取决于考试分数。<br />
<br />
黑领的特殊之处是已经走向组织化和正在走向世袭化，前者巩固，后者继承。在白领黯然陨落之后，黑领的低调崛起在全社会引发了一轮又一轮的考公务员热。同时，黑领也成为所有商家追逐的目标，他们比白领具有更真实更强悍的消费力。他们走到哪里，哪里就物价飞涨；他们对地产的投资，使农民失去了土地，使白领丧失了家园。<br />
<br />
当白领遇见黑领，立马被压出西装下面的“小”来。今天，一个供职于夹缝状态私企的所谓白领，以他微薄的收入仅够维持温饱而已，消费对他来说已经是一个太过夸张和绝望的词语。不久前官商云集（没有几个身家低于千万）的两会上，一个黑领代表或是同情或是鄙夷地建议小白领们应该去卖肉――不是出卖自己的肉体，是卖猪肉。<br />
<br />
在这场席卷地球的金融风暴中，无数外企破产倒闭、业绩滑坡，覆巢之下，纷纷裁员降薪，白领们仓皇失业。与此相反，中国官有组织却财大气粗逆市飘红，令世界500强为之羡慕，黑领们仍然可以毫无罪恶感的集体加薪。<br />
<br />
近水楼台先得月，砸向黑领掌心的4万亿投资计划如同一针鸡血，使无数红了眼的黑领们激动得加额称庆――还是中国好、组织好啊。说实话，贫困潦倒的白领们从这4万亿民脂民膏中想捡点残羹剩饭也是痴心妄想。所以说，“孔乙己”这样卑微的白领如何能与“假洋鬼子”这样傲慢的黑领同日而语？<br />
<br />
如果说白领曾经掀起一股托福热、小资热的话，黑领的江湖则使传统国学和势利文化大热。易中天的阴谋学、王立群阎崇年的帝王学、于丹的犬儒学和马未都的收藏学等等，无不映照了黑领这个社会核心消费阶层的形成。<br />
<br />
黑领的兴起说明，20年前那场轰轰烈烈的反腐败反官倒运动遭到残酷镇压后，新兴知识群体在与权力群体博弈中已经完全丧失了主动权。权力经济终于在近10年从量变到质变，完成了对知识经济和自由经济的彻底颠覆。权力组织在文革后重新收复了对共和国的垄断话语权。<br />
<br />
近年来热映银屏的《激情燃烧的岁月》、《军歌嘹亮》、《金婚》和《天下兄弟》等剧，集中反映了文革时期第一代黑领的优裕生活。权力特权下的文革被营造被演绎得无比温馨富足和谐，根本看不到知识阶层生不如死和农民阶层食不果腹的悲惨灾难。这种以主旋律色彩出现的怀旧情绪充满复辟邪恶和美化罪恶的企图。<br />
<br />
曾经的党校高材生、当代厚黑学大师冯仑老板毫不客气地把白领鄙视为“房奴”，一个“奴”字撕下了一群人看似体面的假领。诚然，白领没有任何社会权利，没有罢工权，没有选举权，没有话语权；他们没有权势，没有资本，没有门第。相反，黑领则是这个国家的上帝选民。他们的房子票子车子等等除过老婆之外，都一概享受无偿配给，几乎不用跟“普通老百姓”们争来抢去的所谓市场发生任何关系。<br />
<br />
白领是如此脆弱而不堪一击，一套小小栖身的房子就可以将其压垮；而黑领是如此坚不可催固若金汤，一场导致无数孩子死亡的“三鹿”惨案，也未见一人因职务犯罪被追究法律责任，仅仅纪律处分了事。<br />
<br />
因为对立法权和司法权的把持，黑领群体才是名义上和实质上的共和国公民，他们普遍享受到一个共和国公民所应当享受的一切政治权利。从基本人权、财产权、公民权、选举权和一切社会福利，他们都应有尽有的得到了充分保护和满足。<br />
<br />
与之相反，日渐普遍和经济失宠的白领群体则无法享受到基本人权保证，更遑论公民权和社会福利。他们被官方称之为与“公民”对立的“普通老百姓”或者“群众”。相对于“共和国公民”而言，“普通老百姓”在政治层面和法律意义上，仅相当于“人畜”、“奴隶”或者“机器人”。他们经常被官方作为十几亿的巨额国家财产来看待，说好听点叫作“劳动力资源”。其对外的称呼为“人民”，多用在“伤害中国人民感情”的时候。<br />
<br />
白领的陨落代表着知识精英的穷途末路和理性精神的落败，黑领的兴盛代表着权力意识形态的扩张，和反知识重权力的血统论和阴谋论王者归来。“知识贬值”必然带来“读书无用论”的盛行，中国社会从此向封建资本主义进一步靠拢。社会文化日渐沙化和盐碱化，重归流氓文化和宫廷权谋黑幕政治的覆辙。<br />
<br />
黑领对白领的阻击和绞杀使构成未来社会主流的新兴中产阶级胎死腹中，建立宪政公民社会的启蒙运动被迫土崩瓦解。这种财阀与权贵的合力扼杀使一个民族的创新能力和创造力严重退化直至丧失。社会结构和信息结构进一步被凝固被肢解，青年一代被年迈保守的既得利益者压制封堵在社会最底层。健康的社会流动和财富循环陷于停滞，推动社会进步的活力和源泉被窒息被堵死。<br />
<br />
胜者为王的狼图腾文化、不择手段的官场权谋文化、暴殄天物的面子文化和崇高伟大的满清皇帝戏之所以大行其道，正映射着白领规则的陨落与黑领规矩的升起，中国社会由知识和文明的艰难复苏，无可挽回地退回到野蛮与无知的权力通吃、弱肉强食中去。<br />
<br />
人往高处走，水往低处流。在全社会的羡慕、嫉妒和仇视之中，黑领阶层一方面继续低调的巩固其社会地位(政治地位和经济地位)，另一方面在完成原始积累后，他们开始悄然向新大陆挺进――携款外逃，或者投资移民，实现自己正式加入世界发达国家高级人类的梦想，同时也使自己的后代永远彻底的摆脱水深火热的中国。</div>

							
							
							
							
