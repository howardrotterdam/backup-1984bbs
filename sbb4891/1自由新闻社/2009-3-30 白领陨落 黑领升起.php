 <br />
<br />
　　新概念振兴方案出来之后，我认为这个钱只会多不会少，因为政府急，希望能透过银行融资来提升企业的经营水平或经营能力，可是经济理论是改变不了的。如果有新概念出来，配合上这么庞大的资金量在社会流转，你还有一轮的机会，但那又是短期的。大家要学聪明一点，要快进快出，而且我发现这个时候是大盘掩护中小盘，连基金我都怀疑他是被动的受害者，所以股民如果想再进的话，我建议不要以基金为主体。如果实在穷极无聊有时间的话，按照我的思路，先判定银行融资够不够，如果银行融资大量放出的话，只要政府有新的利好消息一出来，就要针对有关的行业进行短期冲刺，我相信是无往不利的。而且这一波上升本身就很奇怪，像医药版块本身就没有什么道理，比如说，医药改革方案出来，医药价格会降低，所以医药公司利润也会下降，股价也应该会下跌的，这才比较合理。但是有一些股票大涨，甚至涨了当初6000点的水平，到底是为什么？这一次我判定和外资有关，因为医药版块会成为外资企业09年收购重镇，如果收购了我国的医药企业，外国的药品就顺利进入中国的医疗市场，进而合法的定价，合法的出售，而我们很多中国人需要好药，这些药品一进来，加上外资撑控一种运销渠道，一定能大赚其钱，这就有可能会使得他们在这个时候大量的进货，来控制医药版块，为下一个阶段外资大量并购预先铺路，所以我认为很可能下一波就是和外资大量并购有关的行业是一个新兴的版块。外资不但可以透过撑控企业的股权赚钱，同时通过股价的操纵利于未来的收购和资本运作。当然，我自己是不炒股的，这是我把研究之后的一点心得告诉广大股民，我也希望如果有人听了我的话赚钱了，请注意我的一句话：郎教授是金融博士，但从来不买金融产品的，越复杂的越不买，越有概念的越不买，合同越厚的越不买。<br />
