													<h2>解放军驻津巴布韦钻石矿 勾结穆加贝 军火换血钻</h2>
						
						
																											<div id="postmessage_836884" class="t_msgfont">【苹果日报】<a href="https://hnews.org.ru/apples/index.php?sec_id=15335" target="_blank">https://hnews.org.ru/apples/index.php?sec_id=15335</a><br />
<br />
<img src="http://i55.tinypic.com/2ebv6ro.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
在非洲国家津巴布韦（ Zimbabwe）东部偏僻小镇，几百名士兵携带狼狗巡查，一发现有人闯入就杀无赦。但这些士兵是黄皮肤的中国解放军。解放军坐镇这里是因中国跟津巴布韦大独裁者穆加贝（ Robert Mugabe）进行全球最大血钻交易勾当，把估值 8,000亿英镑（ 9.7万亿港元）的钻矿给解放军合作开采，用钻石换中国军火，来镇压国民和自肥。<br />
<br />
津巴布韦东部邻近莫桑比克的马兰吉（ Marange），在群山围绕下外人罕至，却遍地钻石，多到给当地小童用来射雀，四年前这消息传了出去，大批在严重通胀下变成赤贫的专业人士和工人，都涌来掘钻，希望可从此脱贫。但消息也引来穆加贝军队垂涎，两年前血腥占领矿场，杀了 400人。<br />
<br />
藏钻量值 9.7万亿港元<br />
<br />
穆加贝除了用钻石换中国军火，还让解放军负责马兰吉钻矿的保安，目的是壮大自己实力，为发动新一轮全面夺权作好准备。<br />
<br />
专家相信马兰吉是史上最大钻矿，藏钻量可占全球逾 1/4之多，估计总值 8,000亿镑。不过穆加贝的军队将钻矿据为己有，把那里变成军事区，设下三层围栏，外围布满军营，几百士兵携恶犬巡逻，外人闯入随时被活活打死和撕咬成肉块。<br />
<br />
今年 2月，有报道指穆加贝的军队在马兰吉秘密建机场，估计是为与中国进行血钻换军火秘密交易。英国《每日邮报》记者马隆（ Andrew Malone）最近亲到当地采访，发现这不道德交易的铁证，更发现中国参与的程度很大，解放军直接参与采钻和保安，又参与矿场的血腥恐怖统治。<br />
<br />
该报取得的秘密文件，披露获授权采钻的姆巴达（ Mbada）钻石公司，挂名老板是穆加贝的前私人直升机机师姆兰加（ Robert Mhlanga），姆兰加虽全无采矿经验，但得穆加贝宠信，亲委他出任这要职，公司另外五名合伙人姓名译音分别是邓鸿彦、张士彬、张辉、江兆耀和成清，据说都是中国军官。<br />
<br />
运钻石飞机路线不留纪录<br />
<br />
马隆又指矿场内，一队队黄皮肤的中国军方采矿专家在工作。在已建了巨大跑道、先进雷达仪器和控制塔的机场，有「安 12」运输机升降，每周两次，从首都哈拉雷（ Harare）以外的秘密军事基地，运载器材和人员到矿场，当中包括八至十名中国军官，负责监视驻矿场的津巴布韦军人，也监视在枪口下像奴隶一样工作的当地工人，运输机飞走时，带着价值百万美元计的未切割钻石离开。<br />
<br />
马隆指单是今年，至少有四架飞机从马兰吉的秘密机场，载着这些国家财富直飞中国，但这些飞机的飞行路线，都不留纪录。<br />
<br />
报道指，中津的军火换钻石秘密协议，是由穆加贝的军头亲信奇温加（ Constantine Chiwenga）去年访华时敲定。奇温加连采钻都交给中方，因为他相信中国人比较「守纪律」，可让他和已在亚洲的秘密银行户口收藏逾 15亿镑（ 182亿港元）的穆加贝，私吞更多钻石收益。<br />
<br />
津巴布韦一名情报高官向马隆证实，中国承诺向津巴布韦供应军火：「中国跟津巴布韦签了备忘录，北京给我们武器，我们让他们开采钻石。这是政府之间的协议，是由最高层签署的。」<br />
<br />
情报高官不讳言，津军准备用中国军火，向政敌和反对者发动新一轮血腥镇压，又坦承没有这协议，军政府可能要倒台。他更豪言：「你可以写 1,000篇报道，刊登 1,000次，但结果一样。我们有了钻石，就有武器，可尽杀想跟我们争东西的人。」<br />
<br />
为了保护钻石矿这条财路，解放军和穆加贝的军队不惜泯灭人性，滥杀无辜。村民指解放军竟然放狗，当着一对父母面前咬死他们的女儿。至今最少有七人因为接近钻矿，被解放军杀死后弃尸荒野。<br />
<br />
「这些中国人铁石心肠」<br />
<br />
马兰吉矿场隐藏着许多见不得光的秘密。为禁止外界取得任何线索，当局严禁村民走近半步。但小朋友不知危险，一名女童有一次走近矿场，解放军放狗狂咬她，父母迟一步赶到，只能眼睁睁看着女儿被咬死。<br />
<br />
村民西班达（ Sibanda）也因为走近矿场，被解放军放狗咬，背上还留有伤痕。他说：「这些中国人铁石心肠，他们抢走可以拯救这个国家的钻石，我恨死他们。」<br />
解放军不但向穆加贝提供军火，据说还负责训练穆加贝的军队。津军不少高层成员曾到中国南京军事学院和北韩受训，接受虐待手段。津军射杀平民、奸淫掳掠从不手软。<br />
<br />
津巴布韦一名资深记者说：「穆加贝的军队是聪明的刽子手，因此十分可怕。」<br />
<br />
穆加贝是黑暗政权，为了打击异见者，设立了多达三个情报组织，雇用 10万多名特工。至今有无数反对派政客被杀害，即使手无寸铁的平民，一旦知道军方的秘密，也会招来杀身之祸。<br />
<br />
英国《每日邮报》</div>

							
							
							
							
