<br />
&nbsp; &nbsp; 在北京通州第一实验学校，来自农村的孩子们听了《红旗三部曲》后表示，他们很长时间没有听到这样的好歌了。老师们说，我们太需要这样的主旋律歌曲了，爱国主义教育就是需要这样的教材。<br />
