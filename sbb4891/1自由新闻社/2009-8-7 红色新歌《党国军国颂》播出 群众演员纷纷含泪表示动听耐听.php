<br />
《红旗三部曲》是由北京大学、中国人民对外友好协会、八一电影制片厂联合创作的音乐组歌。组歌分为《党旗颂》《军旗颂》《国旗颂》。组歌词作者为北京大学教授赵为民、军事科学院原政委张序三、中国人民对外友好协会会长陈昊苏，著名作曲家遥远作曲，演唱者为著名歌唱家谭晶、戴玉强。组歌已被制作成MTV作为６０周年国庆的献礼。<br />
<br />
&nbsp; &nbsp; 赵为民教授表示，今年是新中国成立６０周年，是深入进行爱国主义教育的重要契机。同时，在青年学生和全社会中，推广民族的、振奋人心的文艺作品，倡导昂扬向上的精神追求是时代的需求。《红旗三部曲》是一部庄严的、昂扬的、主旋律的时代颂歌，其创作意图是要通过对党旗、军旗、国旗的歌颂，表达人民群众对党、对人民军队、对祖国的热爱。组歌体现中国共产党、中国人民解放军、中华人民共和国一脉相承的联系，并选择旗帜作为他们的象征，以此为载体，通过歌颂党旗、军旗、国旗三项标识性的形象来歌颂党、人民军队和伟大的祖国，并以此作为６０年国庆集体主义、爱国主义思想和情感抒发的重要方式。<br />
<br />
&nbsp; &nbsp; 据介绍，《红旗三部曲》以奏鸣曲式的壮丽交响铺展历史，以宫调大三和弦的音乐织体贯穿始终，以史诗般、情景性的动人旋律描绘８８年来中国共产党的辉煌历程、８２年来人民军队的英雄史诗、６０年来中华人民共和国的坚强崛起。 <br />
<br />
