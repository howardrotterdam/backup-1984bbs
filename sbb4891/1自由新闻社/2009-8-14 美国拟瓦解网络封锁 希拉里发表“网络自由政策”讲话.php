																											<div id="postmessage_202989" class="t_msgfont">美国测试新型网络技术 专门瓦解网络审查<br />
路透社<br />
2009年 8月 14日 星期五 12:03 BJT 保存为书签 | 打印 | 阅读全文 [-] 文字大小 [+]<br />
<br />
路透波士顿8月13日电(记者 Jim Finkle)---美国政府正在中国和伊朗秘密测试网络新技术，该技术可以让两国网民突破当地政府为限制新闻资讯而设置的网络屏障。<br />
<br />
美国广播理事会(Broadcasting Board of Governors)IT部门负责人柏曼(Ken Berman)表示，这项名为 &quot;feed over email&quot; (FOE)的系统通过可以回避网络限制屏障的技术进而传送新闻、网络广播以及数据。理事会负责对该系统进行测试。<br />
<br />
这些新闻资讯将通过电子邮箱传递，其中包括谷歌的Gmail、微软的Hotmail以及雅虎的邮箱。<br />
<br />
柏曼表示：“我们有人在中国和伊朗进行测试。”柏曼所在单位也负责经营“美国之音”。对于这个尚处于测试早期的系统，柏曼没有提供什麽细节。他表示为避免两国政府侦查发现，保密工作很重要。<br />
<br />
参与研发FOE的Sho Ho在邮件中表示，只要稍微对此系统作个调整，多数手机均可使用该系统。<br />
<br />
美国政府还提供一项免费服务，这项服务可以使海外用户获得任何网站的进入途径，包括反美网站。<br />
<br />
柏曼表示：“我们不会在用户访问的网站中发表任何政治声明，我们只是努力传达这样一种价值观：你知道得越多越好。民众可以自己看。”<br />
<br />
除了中国和伊朗，FOE技术的目标国还包括缅甸、塔吉克斯坦、乌兹别克斯坦和越南。（完）<br />
