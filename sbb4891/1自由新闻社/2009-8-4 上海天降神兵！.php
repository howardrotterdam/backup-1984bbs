一条帖子引爆乌龙<br />
<br />
　　在此番抓捕行动当晚10点43分，谢青松的房东王先生随即在爱卡网上海分会社区发布了第一条帖子，指称警方“破错门，打错人，还好意思上电视再邀功！”王在帖子里公布了自己与谢青松等三人的手机和姓名，表示对帖子内容“负法律责任”。<br />
<br />
　　这条名为《今日警方特大行动――658名警察抓获65名罪犯的真实情况！》帖子迅即引起巨大关注，截至被删除前，一两天内跟帖分页达62页之巨。<br />
<br />
　　谢青松称，当晚12点左右，即有看到此帖的上海媒体记者和他联系，次日更是达数十家媒体，但可惜的是，鲜见更正和后续报道。<br />
<br />
　　沪上一位刑辩律师也在博客里提出了自己的质疑：即使情报准确，警方在确定被抓捕人员未携带凶器的情况下，是否有必要破门而入？警方已经当场发现并确认了错误，却不启动错误执法的应急预案，安排专人主动介入，及时纠正妥善处理。反而让媒体作为正确执法的新闻予以播报，实在是错上加错。<br />
