													<h2>上海乌龙剿匪记</h2>
						
						
																											<div id="postmessage_203049" class="t_msgfont">上海乌龙剿匪记<br />
<br />
2009-8-14 10:04:51　来源: 南都周刊　浏览量: 3954　网友评论 5 条 推荐此文<br />
<br />
南都周刊主笔・石扉客 实习生 王丹阳 上海报道<br />
 <br />
<br />
“你们是在搞反恐演习吗？”<br />
<br />
　　确如事后上海当地媒体报道所说，“这个过程就像一个默片”。全副武装的特警从三楼阳台潜入房间时，正在上厕所的陈峰毫无察觉。<br />
<br />
　　穿着三角裤头的他，刚从马桶上站起来，卫生间的门突然被一脚踢开，只听一声断喝，“趴下不许动。”一条大汉闯了进来，头蒙黑纱，手持警棍。他惊得“扑通”一声跌坐回马桶上。<br />
<br />
　　只见一道黑影掠过，背上被结结实实抽了一记，陈峰完全蒙了。从《新民晚报》当天刊登的现场照片上可以看到，上身赤裸的陈，双手抱头，蹲在办公室格子间里，三个全副武装的特警在周围紧密控制着他。<br />
<br />
　　与此同时，谢青松也醒了过来。谢是陈的大学同学，在这里开了个小公司，当晚他和女友李玲睡在这套复式办公室的二楼。<br />
<br />
　　尚在恍惚中的谢隐约听到楼下有嘈杂声，声音似乎越来越大。他刚准备爬起来察看动静，房门也被一脚踢开，一群人冲进来。谢惊得从床上跳起，李玲也吓得尖叫起来。没有人亮身份，谢青松琢磨到底是抢劫还是绑架。<br />
<br />
　　李玲被押下楼的时候，还穿着睡衣，边走边抽泣。被控制在房间里的谢青松，听到下面一楼传来欢呼声，“就听见有人叫‘啊，又来了一个’，然后是咔嚓咔嚓的拍照声。”谢说。<br />
<br />
　　三人都被带到办公室一楼，双手抱头，靠墙蹲下。“什么都不许问、不许做”。这时有警察进来查问身份证。“抓错了”，谢听见有人说了一句。谢是上海户籍，陈峰有居住证。等他抬起头，发现不一会儿功夫，特警模样的人全部撤了，屋里只剩下2个警察。<br />
<br />
　　谢青松从楼梯附近的窗户往下一看，他这个位于三楼的房间外面赫然架着一台云梯，不知何时，楼下已站满了两排全副武装的特警，一个领导模样的人正在跟他们挨个握手。他这时已经确认，刚才闯进家门的是警察无疑，正在犹豫是否找相机把这幕拍下来。<br />
<br />
　　这时有人在房门外叫了声“收队”，留在屋里的两个警察转身也准备走。谢青松鼓足勇气拉住其中一个说，你们把我们人也打伤了，门也踢坏了，不能就这么走了吧？总得给个说法啊！对方说没有办法给你解释，我们是803，会有人来赔偿的，留下了一个电话号码。<br />
<br />
　　谢青松忍不住追上去问了句，你们是在搞反恐演习吗？一个警察摇头，回头对他说了句让他印象极深的话：你今天看一下东方卫视吧。<br />
<br />
　　办公室没有电视机。惊魂未定的谢陈二人，让同学留神帮着收看东方卫视。接下来陈峰去医院验伤，谢青松带着李玲去莘庄散心，身心俱疲的这对恋人“晚上狠狠吃了一顿，补了下元气”。<br />
<br />
　　这个所谓的“默片”其实是一个“好莱坞大片”。<br />
<br />
　　上演的时间是8月4日凌晨4点多，地点是上海市南郊银都路2688弄的一个商住小区。<br />
 <br />
“警察喝我的王老吉！”<br />
<br />
　　和李本传等的遭遇相比，谢青松其实该庆幸，也许因为他的白面书生模样，他至少没被拷，甚至没碰到殴打。<br />
<br />
　　李本传是安徽肥东人，在沪打工多年，花了4000元租下这个小区里近200平米的两套房子，给自己的物流公司办公兼居住。<br />
<br />
　　办公室设在1201，他的中学同学陈刚做业务员，住在这里；相邻的1202也是个三居室，李自己住一间，妻舅孔凡青过来做司机，和妻子王义兰合住一间。还有一个叫张小峰的大学毕业生，租住了剩下的一间。<br />
<br />
　　李本传的妻子蒋琴，带着两个孩子刚从老家过来探亲。那晚，听到套间的外门被踢开，杂乱的脚步声在客厅响起，房间的门被踢得巨响。两夫妻爬起来拼命堵门，李大喊“救命”，夫妻俩都以为是黑恶势力来袭。<br />
<br />
　　房门很快被踢破，至今清晰可见拦腰一道大裂痕。冲进来的人抡头就打，李被反铐着押出房间。两个孩子都醒了，小的3岁，呆住了，傻站在床上；大的是女儿，才8岁，吓得直哭。<br />
<br />
　　陈刚听到了李本传喊救命，刚从床上跳起来，门就被踢开，挨了一警棍后，也被拷起来。他伸出双手，让记者看被拷的伤痕，然后缩回手揉自己肚皮上的一道青痕，说是被警棍捣的。<br />
<br />
　　加上孔凡清和张小峰，四个男人都被拷起来了。一个全副武装的警察随手打开冰箱，拿出一瓶王老吉喝起来。<br />
<br />
　　有人进来查问身份证，几番喝问后，特警都撤了，屋里只剩下三个警察看着，1201室一个，1202两个。手铐都是双手反拷在背后，四人中年龄最大的孔凡清实在受不了，央求留守的警察打开手铐。<br />
<br />
　　看到丈夫和舅舅的手铐打开了，蒋琴这时已经大致猜到是警察搞错了，忍不住质问“你们是警察还是土匪？”对方回答“我们不是土匪，我们是特警”。“你们不是特警！特警干嘛喝我的王老吉？”蒋大骂。<br />
<br />
　　这时门外有人打手势招呼。“他们叫‘跑路’！”陈刚伸出两个手指头比划。<br />
<br />
　　于是看守的三人也起身就走。几个刚解铐的男人追到楼梯口讨说法，“要告，你们去告，我们是803。”对方说。<br />
 <br />
“你没事了，不要往外乱说！” <br />
<br />
　　李本传还算幸运，虽然被打被拷，但都当场获释。与之相比，住在2688弄××栋三楼某室的张庭张愉兄弟俩就没这么走运了。<br />
<br />
　　丽萍发廊（化名）的13个员工群租在这套100平米的房间里，摆放着十余张床铺。在这家发廊打工的张庭和张愉兄弟俩就住在这里。<br />
<br />
　　他们都是8月4日晨直接从床上被抓走的。穿着裤头的张庭，警察把五个书包和袋子挂在他脖子上，他估计对方认为那是赃物。俯卧在床上睡得正香的弟弟张愉，被人从背后揪着头发拽到地上拷起来。<br />
<br />
　　兄弟俩和另外5个同事都被戴上黑头套，押到附近的一家警察培训基地。“很嘈杂，听着像是个大食堂”，张庭努力回忆，似乎所有被抓的人都被带到此地会合，然后横穿大半个上海，押送到上海北部的虹口分局。<br />
<br />
　　张庭是关了一宿之后的次日下午，自己坐地铁回来的。“经过初步调查，你没事了，不要往外乱说。”他记得警方最后跟他说的一句话。<br />
同一天中午，弟弟张愉和另外一个同乡吴军也放回来了。张愉说，放出来时自己连坐公交的钱都没了，发廊经理曹小姐替他们支付了近90元的士车费。<br />
<br />
　　但张愉还是觉得自己比哥哥走运，他虽然丢了一双鞋，但至少脖子上没挂书包，讯问他的警察还帮他找到了抓捕时被拽掉的金项链，而他哥哥的金项链被拽下后就丢了一截。<br />
<br />
　　兄弟俩都饿了整整一宿。那一晚，哥哥张庭记忆最深的是被吊拷在铁栏杆上，只能脚尖着地，“那时真是太难受了”。<br />
<br />
　　兄弟俩是江西赣州人，初中毕业后都在当地一家文艺学校上学，哥哥张庭学舞蹈，弟弟学英语，没读几年就先后出来上海打工，到出事时，哥哥在这家美发中心已经干了半年，弟弟就跟着过来帮着打杂，才一个多月就出了这事。“我弟弟是1992年3月出生的，还未成年呢！”这个瘦削的哥哥低下头，声音越来越低沉，几如蚊吟。<br />
<br />
　　他们的经理曹女士最担心的，是第三批被抓进去的员工，祝辉、邓志平、何青云、张强强等四人，其中一人身体不太好。5日晚饭时分，这四人终于回来了。曹说，被抓走时好多人只穿着裤头，放回来时“还是穿着三角裤回来”。<br />
<br />
“十年来规模最大的一次抓捕行动”<br />
<br />
这三户人家最终碰到一起，是在小区附近的田园新村派出所。在那里，他们听到最多的一个词是803。<br />
<br />
　　803是上海刑事警察总队的代称，因位于中山北一路803号而得名。这个简称在上海本地居民中几乎家喻户晓。谢青松他们此时方知，这个案子正是威震华东的803办理的，而他们有幸遭逢了上海警方近十年来规模最大的一次抓捕行动。<br />
<br />
　　据上海公安网当晚发布的消息，这次行动是上海警方开展“迎世博、保平安”打击整治攻坚战的一部分，实际上缘起5月下旬虹口分局侦破的一起盗窃案，涉及到一个盗窃团伙。<br />
<br />
　　上海公安网称“该团伙成员来自江西吉安，部分成员居住在闵行银都路某宾馆，而居住于附近一小区内的其他成员则经常到该宾馆4楼棋牌室赌博。侦查员忍着高温和蚊咬，在宾馆周边开展了24小时不间断监控，获取了大量有效信息，也查明了该团伙外出作案的规律。”<br />
<br />
　　据此，专案组精心策划集中抓捕方案，由上海市公安局调集包括“刑侦总队、特警总队、公安高等专科学校、虹口分局、闵行分局等五家单位共计600余名警力成立联合行动组，最终于8月4日凌晨将该犯罪团伙一网打尽。”<br />
<br />
　　新民晚报社随警记者潘高峰在做客“新民网”时回忆，“803刑警说当晚只要是男的，几乎全都上了。兵法云‘十则围之’。”“而此前先不抓他们，是为了更好地打击他们，警方是放长线钓大鱼。”<br />
<br />
　　潘高峰称自己是在场的唯一平媒记者。他说抓捕时机是警方精心选择的结果，因嫌犯总是找晴好的天气出门作案，而前段上海连续阴雨，4日刚刚放晴。<br />
<br />
　　而抓捕对象的主要特征之一即是江西吉安籍，由此也能解释谢青松等3名上海和江苏户籍，李本传等8名安徽户籍人士当场获释，而张庭等7名江西人被带走讯问。<br />
<br />
　　当天行动中包括银都路在内，全市共有五处抓捕点。潘高峰称，谢青松所在的房间是警方锁定的窝点，情报十分准确，因能突出新闻性，也是当晚媒体聚集和集中报道的地方。<br />
<br />
　　据上海公安网当晚发布的消息，此番行动最后总抓获65人。而南都周刊记者调查发现，包括上述三家在内，仅银都路2688弄一处抓捕点，当天至少有6处房间被错破门而入，30人被惊扰，其中12人被错拷，8人被带走后放回。<br />
<br />
　　此番乌龙何以铸成？潘分析，2688弄小区本是在沪江西人的一个聚居区，群租和混居杂居现象严重，有可能是犯罪嫌疑人曾经在该小区租住过，情报没能及时跟上导致失误。<br />
<br />
　　南都周刊记者现场调查发现，三处抓错的地点，均与警方抓捕目标十分接近。谢青松等3人、张庭等7人的住所，和警方抓捕目标某小饭店对面的205室，均在同一栋楼，相距不过数十米；而被错抓的李本传一家的楼上，即是警方原定的两家抓捕对象1301室和1401室。<br />
<br />
　　银都路2688弄，位于上海闵行区的外环以南，有40来栋别墅式公寓，2004年刚竣工，楼层大多为四层。每栋楼的底层都是一创意主题区，如“15栋为睡眠中心”、“20栋是经典家具”、“38栋系橱柜家电”。其内部则特别符合平时所见的“城中村”之景，四处可见家庭旅馆、杂货铺、发廊、快餐店、发货仓、三五一伙的私营公司等。<br />
<br />
　　物业管理处肯定地说住在这里的“70%不是上海人，浙江老板多，安徽、江西的更多。”这里50%都是物流公司，记者发现，货仓、办公大多集中在中厅，而生活起居就在如山的货架背后的小房间里，基本上一房几人住。<br />
<br />
　　而警方这次十年来规模最大的抓捕行动，恰恰就选在这个奇特小区，乌龙也在此铸成。<br />
 <br />
警方和媒体的迎合<br />
<br />
　　等到当天下午6点多，四处找电视的谢青松和陈峰，终于在当地电视台的新闻里知道了原委，也看到了画面里狼狈不堪的自己。<br />
<br />
　　“那个穿黑T恤抱头蹲下的是我，套着三角裤头的是陈峰，穿着黄色睡衣从楼上被押下来的就是我老婆??”谢青松的最大痛苦在于，那幅场景在电视里被完整地曝光了，“去交电费时，物业的小姑娘都看着我嗤嗤地笑。”<br />
<br />
　　当地报纸的报道也让他困惑。对报道里“如猿猴般敏捷、如闪电般迅疾”的特警，一分钟多“连破两个玻璃防盗窗”的说法，谢青松嗤之以鼻。这套复式房的阳台其实没有防盗窗，铝合金推拉玻璃门并没锁上，实际上警察从云梯跳进阳台后，轻轻拉开阳台上推拉门就进来了。<br />
陈峰则对报道里说的“四名犯罪嫌疑人束手就擒”大惑不解，这套房子里明明就他们三个人。<br />
<br />
　　对报道中的描述“外科手术式的精确打击”，孔凡清觉得这简直是笑话，“有这么做手术的吗？打烂这么多地方，抓错这么多人，一场严重的医疗事故还差不多！”<br />
<br />
　　潘高峰并不认为自己的报道失实，因为他“在现场停留的时间段内情况的确如此”，但潘承认其实并未和特警同步进入现场，其主要信息来源是在抓捕之后采访特警所得。<br />
<br />
　　潘所在的媒体当天下午3点即上摊，应是最早报道警方此次突袭行动的媒体。潘称直至见报前没收到任何来自警方的提示消息。<br />
<br />
　　当晚上海本地数家电视媒体即播出了这条新闻；次日一早上摊的上海其他日报也随即刊登了该次行动的新闻，大多题为《上海实施十多年来最大规模抓捕行动》。和潘文的区别是，这些报道大都隐去了小区名或具体单元房间号，以及夸张的抓捕情节。<br />
<br />
　　另外一个区别是，这些报道都加署了“通讯员忻文柯、庄莉强”。事实上忻文柯即上海市公安局新闻科的谐音化名，庄是该科科长。业内人士分析，这些稿子应是由警方统一提供给媒体使用的。<br />
