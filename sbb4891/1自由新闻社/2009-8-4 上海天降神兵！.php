<br />
　　除了62部手持电台，直面犯罪嫌疑人的民警还领到了防刺背心，特警则全副武装。昨天凌晨2时许，17辆“运兵车”离开刑侦总队大院，驶往“屯兵点”――普洱路上一所中学，此地距嫌疑人聚居小区约10分钟路程；比他们离“敌人”更近的是此次行动的前线指挥部，就设在与聚居点一路之隔的一家商务旅馆里。<br />
<br />
