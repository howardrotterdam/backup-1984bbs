梁文道：韩寒是下一个鲁迅。<br />
青年时报6月24日报道<br />
<br />
<br />
 这种天气，在一个没有空调的室内场地办讲座是不可思议的；办了讲座还能挤得满满当当的，那就更不可思议了。但是，梁文道可以。昨天晚上的枫林晚书店就是这样的场面，用来降温的冰块和听讲者脸上淌下的汗水，把场地弄得不但闷热，还有些潮湿，却并不妨碍人们不时为梁文道的精彩演讲鼓掌叫好。演讲内容非常宽泛，从读书的方式直讲到作协的作用，“金庸入作协不算什么，要是韩寒当了作协主席，我们都会加入的。”说这句话，梁文道可不是在开玩笑。<br />
<br />
韩寒是下一个鲁迅<br />
<br />
《常识》《我执》两本书在内地热销，让梁文道这位香港文化人忽然成了内地的热点人物。人们总希望听听他对各种事件的看法，金庸加入中国作协的事余波未平，听众自然免不了要问一下曾经担任凤凰卫视评论员的梁文道，梁文道却说起了十几年前认识的一个文化人，“名片上印着国家二级剧评人，当时非常不懂，为什么剧评也要分级。”梁文道说，直到后来自己才明白职称评定在内地是关乎待遇的，而加入作协除了待遇，以前还意味着对一位作家好不好的判断，“但是作协的这个象征意义今天已经垮掉了，就像铁凝是作协主席，但她一定不是中国最好的作家。”<br />
<br />
那么金庸在这个时候加入作协是为了什么？“他显然不是为了待遇，也得不到那层象征意义，只能说是他对作协体制的一种认同。”至于有人说作协希望通过这件事来提升影响力，重新塑造象征意义，梁文道又有自己的看法，“金庸不入作协也像作协的人。除非是韩寒这样和作协形象相差巨大的人来接铁凝的班，才有可能达到这个目的。”<br />
<br />
梁文道是极其看好韩寒的，“再写几年他就是另一个鲁迅，他只是少些鲁迅身上的深沉和悲剧感。”梁文道说，如今人们总喜欢说80后怎么怎么，实在是太类型化了，80后其实也不乏韩寒这样有想法的人。<br />
<br />
读经典能读成于丹<br />
<br />
昨天讲座的主题是读书，说起读书这回事，梁文道也有着和内地人几乎相同的读书记忆。在台湾接受中学教育的他，也在路边的书摊里租过书看，也读过“全庸”号称完本的武侠小说(假金庸的书)。<br />
<br />
<br />
“那时候读书是一种可以接受的休闲方式，但现在不是，人们不是没时间读书，而是没把读书放在首位。”梁文道说，时下人们有时间会去唱歌，会去洗脚，因为大家都知道这些事情能让人愉快，读书却不一定。“以前人们读经典，读得非常细致，不是你读经典是经典读你，发现自己是谁。如果用这种方式读经典，读不成圣人，也能读成个于丹。”<br />
<br />
梁文道很推崇华文写作概念，不但有大陆文学、港台文学，还有新加坡、马来西亚等地的华语写作，“很多人讲究华文写作的正统，批评港台文学有港台腔，但从另一个角度来看，港台文学的发展不正是丰富了华文写作吗？”<br />
<br />
<br />
(时报记者王晟/文) (本文来源：青年时报 )</div>

							
							
							
							
