																											<div id="postmessage_162226" class="t_msgfont">广东参事建议控制外来工数量 须高中以上学历才能进<br />
<br />
在昨日举行的广东省政府参事决策咨询会上，省政府参事曾添贵建议，提高珠三角外来农民工的准入门槛，凡进入珠三角就业的外省农民工必须具有高中或中专以上学历，逐步把低素质劳动力边缘化。<br />
<br />
曾添贵是参事政法组成员，曾任省司法厅副厅长、巡视员，昨日作了题为《在国际金融危机中维护我省就业形势稳定的若干建议》的发言。<br />
<br />
农民工入珠三角须有高中以上学历<br />
<br />
曾添贵发言中认为，广东要抓住“双转移的契机”，提高珠三角外来农民工的准入门槛，以政策引导或地方法规的形式，规定凡进入珠三角就业的外省农民工必须具有高中或中专以上学历，逐步把低素质劳动力边缘化，确保我省“三促进一保持”的目标得以实现。<br />
<br />
控制外来务工人员数量<br />
<br />
曾添贵还建议政府准确测算广东资源和公共服务承载能力，控制外来务工人员数量。他认为广东水、电、气、土地等资源和公共服务、社会管理承载能力繁重，对全国富余劳动力的安排，不可能“包打天下”，政府应积极参与对劳动力资源的宏观调控，确保广东经济社会可持续协调发展。<br />
<br />
 网友有话<br />
凤凰网友（广东惠州）<br />
我是珠三角居民，但我肯定反对这样建议。<br />
更多&gt;&gt;<br />
照顾广东籍失业人员<br />
<br />
曾添贵建议出台政策规定，产业转移园招用当地富余劳动力不得低于一定比例，他认为广东应确保粤籍城乡居<br />
<br />
民充分就业，办好32个产业转移园，为粤东、西、北部农村富余劳动力就近转移就业提供平台。<br />
<br />
曾添贵说，为鼓励广东籍失业人员到粤东、西、北部就业，可制定保留原户籍、原社会保障基金、来去自由等一系列便利政策，并在购房、家属子女安置等方面给予照顾。对暂时找不到工作的本省籍返乡农民工和失地富余劳动力加强培训，培训经费由省市财政资助。与此同时，可适当吸收和照顾文化层次较低的外省务工人员。<br />
<br />
大学生摆摊不要看成“走鬼”<br />
<br />
针对大学生就业难，曾添贵建议省出台几条刚性措施，作为临时措施，可参照上世纪80年代“谁家的孩子谁家抱”，党政机关、事业单位对本单位职工子女中的大专毕业生，优先作临时合同工安置。<br />
<br />
此外，他建议允许大学生低起点创业，如在城镇摆摊修电器、修汽车、摩托车、做家教等，有关部门不要把他们看成“走鬼”，而应作为服务型小经济加以扶持。<br />
<br />
专家意见<br />
<br />
符合“双转移”思路 但操作上有难度<br />
<br />
广州市社科院科研处处长、研究员彭澎表示，限制外省农民工的学历，“是符合‘双转移’思路的”，但是操作上有难度，也可能不符合国家有关法规，很难通过。提高劳动力素质，主要还是应该通过政策上鼓励，用市场的力量，不应强制。</div>

							
							
							
							
