<br />
揭秘北京小额洗钱暗流 商场购物卡成送礼热门<br />
2010-06-13 10:28 　来源： 新周刊 作者:　 编辑: 郑莉娜<br />
　　北京的小额洗钱暗流 　　送礼就送“腐败卡”<br />
　　“腐败卡”热销北京，背后是一个已积聚起来8000亿元资金的灰色地带。一张小小的卡片，让洗钱、行贿、商业回扣、企业避税、零息集资等或疑似合法或非法的社会诉求，得到了释放。<br />
<br />
　　<br />
<br />
　　刘春林一屁股坐下去的时候，黑皮小圆凳子忽地往下一沉，这位有着浓重陕北口音的中年大汉，是内蒙古伊泰煤炭股份有限公司的总会计师。此刻，他正坐在北京金融街投资广场八楼营业厅第一排位子上，旁边站着一位同样高大壮实的同事。<br />
<br />
　　“这个卡怎么办？”刘春林声音洪亮。他是听一位业界同行介绍，专程来买这里出品的一种储值购物卡。“你办多少钱的？”柜台里面的年轻售卡员面不改色。“10万元。”刘春林的声调又提了一个高度。<br />
<br />
　　笑容浮现在脸上，与传说中的态度生硬有所差别，柜台里的售卡员开始耐心解答刘春林的各种疑问，唯一无法让步的是：必须要收取3%，也就是10 万块之外，再拿3000元作为手续费，即使对方打了不少电话。<br />
<br />
　　接下来的争论出现在刘春林和同事之间，刘春林打算在这10万块里，买2张3万的卡，再买1张4万的。而他的同事则坚持认为，买3张2万的，一张3万的，外加一张1万的，才更便于合理分配。看来，这道不太难的算术题里，蕴含着不同的利益格局。在他们争论的时候，柜台里的姑娘一直笑吟吟地注视着他们，直到最后方案敲定，售卡员才说，每张最高不能超过1万。“好吧，来10张1万的。”刘春林有些泄气。<br />
<br />
　　与刘春林相隔不远的位子上，同样有位开口不凡的主顾。这位穿着干练的女士，显然是在为单位采购“福利”，“每张1000元，来772张。”说完掏出一张70多万元的支票。柜台里好一阵忙活，整整三大包，售卡人员费了好大劲，才把所有卡片分批递出来。“累死我了，北京人民广播电台的。”看着客人背影闪出转角，售卡姑娘长出一口气。<br />
<br />
　　这里戒备森严，保安和摄像头都比普通银行营业厅多出一倍，但这位于金融街写字楼内的营业厅并不是银行，只是一间担保公司。大家慕名而来，想要购买的那张神奇卡片，全名叫做“北京商业服务通用积分卡”，简称“商通卡”，说是积分卡，里面存的却是实打实的现金，可以在京城内大多数商场、餐饮、超市、健身、体检、医疗、美容等场所刷卡消费。除了使用范围广，人们看重它的最大优点是：不记名、不挂失，递送往来过程中，神不知鬼不觉，不仅成为“送礼” 的良好选择，亦成为洗钱、企业避税发放员工福利的好载体。所以人们又称呼它们为“腐败卡”、“行贿卡”。<br />
<br />
　　在北京的储值卡市场上，除了资和信担保公司发行的“商通卡”，现在还流行着家乐福卡、北京华联卡、开联连心卡、润京卡、瑞通卡、商联通卡等同类性质的卡片。而在上海比较盛行的则是联华OK卡、雅高卡、一城卡等。一城卡除了可用于购物，支付水、电、煤气费用等，甚至也可以将余值充值到其他同城储值卡里。<br />
<br />
　　上述储值卡每张上限一般为10万元，皆可根据客户需要按照“一百的倍数”定制面值，所以，在商通卡柜台前经常能听到一些并不齐整的数字，“买一张3200元的”或“我存8700元”。刘春林或许有所不知，他跟同事敲定的方案原本是可以实施的，只是最近，资和信公司为了规避“争议”，把每张卡的储值金额“关闭”在1万以内了。<br />
<br />
　　“做得好一天能收二三十万”<br />
　　出复兴门地铁站，就能听到叫卖声，准确地说，是叫买声。两位女士，一老一少，相隔三米远，叫喊声呼应：储值卡有卖的吗？假若你顺着金融街一直往北走，中途能看到很多青年男子在那里拦车“叫买”。他们的主顾一般躲在车里，停在路边，探出头来交易。<br />
<br />
　　在众多“黄牛党人”中间，于女士算比较和蔼可亲的一位，看上去40岁上下的样子，逢人便叫大哥。“大哥，现在都是九五折。”于女士从一群“黄牛党人”中走出来，边走边小声讨价还价，“我可以给你九六折。”在投资广场楼下，有大批像于女士这样的人在招揽生意，中午吃饭的时候，会有骑着电动车的 “小头目”带着大把现金，从他们手中收取卡片。这些层级分明的工作，为商通卡等各类储值卡提供了来自民间的现金出口，他们通常按照储值卡面值的九五至九七折为客户兑换现金。<br />
<br />
　　“你们收了卡之后是怎么用的？”问题有些唐突，但于女士乐于分享：我们有专门的人，守候在各大商场的收银台附近，遇到有买单的顾客，就说服他们，用我们的商通卡来划账，顾客给我们现金，当然要比直接支付给商场的价钱便宜些，我们赚些差价。显然，这条经营链已趋成熟，“你自己去商场没用，现在人们都有信用卡，我们的人很专业”。于女士告诫道。<br />
<br />
　　如果说投资广场八楼的的营业厅，为不记名资金提供入口的话，那么楼下众多“黄牛党人”，则为这些资金提供了出口。那些收到了卡的人们，假若不愿意在消费场所使用，就可以来这里获取现金，在某种程度上，那张卡片，在扮演着利益输送的工具。<br />
<br />
　　那么，到底有多大规模的资金，在通过这种方式流转呢？《新周刊》记者蹲守金融街4天，分别对利益输送的“入口”和“出口”进行了粗略统计，投资广场八楼的营业大厅有近千平方米，设置了21个标准业务办理窗口。估算下来平均每两三分钟就有一人办理业务。生意兴隆。即使是非节假日的上班时间，顾客也是络绎不绝。“每天打听的人有很多，哪能数得过来。”楼下大堂的保安说。<br />
<br />
　　营业厅内的坐椅旁，37个未开封的纸箱堆在一起，每个箱子上都贴着白色的标签，明确写着：“商通卡(蓝)，张数：1500，正数：50000”。从业务员的介绍中得知，这是2010年元旦之后启用的新版蓝色商通卡，有效期从过去的1年延长到了5年，单卡最多可储值1万元。<br />
<br />
　　一位年轻女士咨询柜台后在大厅走来走去，大声打着电话：“对，开了5张。人家没法开到一起，一张最多1万。”最后，这位女士安慰电话那头DD “舍不得小钱赚不到大钱，少了拿不出手啊！”随即挂断电话。<br />
<br />
　　记者粗略计算了顾客人数，如果按照人均采购3万元计算，这家办卡中心的日售卡金额保守也在数百万元之上。现在，他们又在马甸和西南四环开了分店。来自资和信官方的消息是，2008年的售卡额就已经达到40亿元。<br />
<br />
　　在“出口”方面，君太百货门外一位“黄牛党人”说： “做得好一天能收二三十万，少了也有一万多，来钱比较快，反正这个卡绝大部分都不是自己掏钱买的，九五折就很划算了。”在他的客户里，还有人专门要了联系方式，定期约他上门收购。<br />
<br />
　　“资和信”的半壁江山<br />
　　一位金融行业的权威研究者告诉《新周刊》，北京有上千家发卡公司，但是资和信的商通卡占据了这个市场的半壁江山。在百盛商场生意并不好的年月，每年20多亿元的销售额里，商通卡的消费一度占据了10多亿。<br />
<br />
　　如果你的名下，有一个规模庞大的资金池，人们暂时把钱放在这里，一时半会不会用，即使有消费，但源源不断的预存现金足够应付小额支付，这笔沉淀在你账面上的钱，像水库蓄水一般滞留在那里，你会怎么处理？投资、买国债、炒股，还是开店？<br />
<br />
　　庞大的“沉淀资金”即使存放银行，也可获得不菲的利息收入，更何况拿到资本市场上去运作，上述金融权威研究者曾说，买国债是最理想和稳定的投资方式，短期能就能获得8%左右的收益。但有的人显然没有满足于“保守的收益”。<br />
<br />
　　公开资料显示，从2003年至2007年，资和信集团旗下的三家全资子公司DD北京资和信担保有限公司、北京资和信咨询中心、北京金军资和信风险投资有限公司在资本市场上屡有动作。一个与资和信担保总经理王悦殊同名的自然人，也先后出现在两只股票的十大流通股东之列。<br />
<br />
　　在中国股市起起落落的这5年里，“资和信系”先后投资过中青旅、中体产业、中视传媒、厦门信达、精达股份、上海家化等股票；自然人王悦殊还投资过中青旅和四川长虹。<br />
<br />
　　“我们在上市公司的投资于2006年6月已经全部退出，而且绝大部分是盈利的。”资和信集团一位高管说道。但他并没有解释，一旦投资失利，从购卡者、持卡者、特约商户到“黄牛党”，这条利益链上的所有共同体，如何抵御天量资本风险。<br />
<br />
　　2008年，资和信百货及其网上商城搭建完毕，成为商通卡消费又一平台。不少奢侈品牌进驻商场，但其售价也往往高出一般商场。比如，一台索尼 52EX700平板电视在京东商城等网站的报价约在15000元左右，而资和信商城的报价是19000元，即便其网购优惠，标价也为17380元。但高价并没有影响商场的日益繁荣，自开业以来，资和信百货商场每月的营业额增长率达到20%以上。<br />
<br />
　　“如果你的卡里，只剩三五块钱，你还愿不愿意，专门跑去把它刷掉？”上述金融研究者问道，“我相信很多人都不会在乎。这在金融行业里，叫做残值收益，那些零零碎碎的残值，积攒多了，就成为一笔可观的财富。”在储值卡的盈利模式里，除了投资收益、零售收益、手续费和商场返佣收益，就是储值卡的残值收益了。<br />
<br />
　　“腐败卡”的利益链<br />
　　百盛，央行，投资广场DD在方圆不到一公里的范围内，三家机构呈品字型，坐落在金融大街的南侧。比它们地理位置更近的是，他们在业务上密集交叉的程度。<br />
<br />
　　这是一个政策灰色地带，整个发卡链条中有着许多说不清楚的法律关系，但因其源源不断的需求而屡禁不止。通过间接渠道，《新周刊》获悉，央行支付结算司正在研究对策，对进入货币流通领域内的购物卡片进行监管，但是目前，没有鲜明的对应政策出台。<br />
<br />
　　在整个发卡的利益链上，分别“居住”着发卡机构、购买者、持有卡片者、特约商户、黄牛党。作为发卡的前提之一，特约商户是利益链另一端最重要的关节。北京的另外一家发卡主体DD福卡的技术人员说，一般发卡方都会跟一家家的商户、超市和商场洽谈合作，商场会按照消费数额，按比例向发卡者返佣，具体比例，要根据双方的谈判地位区别对待，以前只有1%上下，现在都升至10%，因为发卡方越来越强势了。<br />
<br />
　　商户返佣作为发卡机构的盈利来源之一，向来备受重视，只有这一切都谈好之后，才会有下一步的技术改造，这就需要IT技术人员出场了，他们一般根据各家商户的设备状况，进行兼容改造。改造的同时，发卡公司就已经开始对外售卡了。售卡也是要收手续费和技术服务费的。<br />
<br />
　　为了给沉淀资金赢得更多的时间，发卡公司一般跟商户按月结算，就是即使消费者在商家这里进行了消费，也得等到过一段时间，资金才能从发卡者的账户上划到商家账上。“我们参与这个卡，要交给他们一定比例的手续费。作为好处，他们会替我们宣传，而且这个卡折现比较难，大家不会像现金一样存起来，可以促进消费者在我们店里消费，促进销售。另一方面，也相当于变相给我们介绍顾客。”北京一家体育用品店的店长说，唯一不爽的是，消费资金不是立即到账。<br />
<br />
　　《南方周末》前记者孙亚菲，现在也是资和信商通卡的特约商户，孙从报社辞职之后，创业办起了“奢侈品销售网站”，主要销售从大型商场下架的高档服装、首饰和箱包用品，因为意识到中国有50%的奢侈品消费都是用于商务送礼，所以孙亚菲千方百计争取到了资和信的合作。客户持商通卡就能在她的网站购物。<br />
<br />
　　这是一条完整有序的产业链和利益链，金融业内人士的估计是，在几乎监管空白的发卡领域里，已经积聚起来8000亿元的资金，通过一张小小的卡片，洗钱、行贿、商业回扣、企业避税、零息集资等或疑似合法或非法的社会诉求，得到了集中的满足和释放。</div>

							
							
							
							
