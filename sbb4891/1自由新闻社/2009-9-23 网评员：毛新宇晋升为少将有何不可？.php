&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;其次，毛新宇完全有资格有能力晋升为少将军衔。毛新宇拥有博士学位，晋升少将前为中国军事科学院战争理论和战略研究部副部长，副军级，大校军衔。话说毛新宇就像是毛泽东的影子，他长相敦厚，身材魁梧，性格沉稳，声音洪亮，着装朴素，家里最值钱的就是毛泽东像，他为人低调，富有爱心。去年地震期间，他还向地震灾区捐款10万元。这么优秀的人，为什么不能晋升为将军呢？<br />
