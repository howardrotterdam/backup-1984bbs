&nbsp; &nbsp;&nbsp;&nbsp;在新中国成立60周年之际，毛新宇晋升为少将，的确是件很令人欣慰的事情。当大多数人在浏览到这条消息的时候，都留下热情洋溢的评论，向毛新宇同志表示热烈祝贺，并希望他时刻保持低调，做一个堂堂正正的共产党人，继续宣传、研究和宏扬毛泽东思想，全心全意地为人民服务。<br />
&nbsp; &nbsp;&nbsp; &nbsp; 然而，笔者在凤凰网看到这样的声音：“真不明白为什么喜欢授予歌手、电影明星、名人之后等与军事无关的闲杂人将军衔，随便授予一些不相干的人军衔是对那些用鲜血和生命换来军衔的军人的不敬。如果出于对老一代革命家的尊敬，应该设立荣誉衔位。”<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;显然，持这种观点的人认为，毛新宇等人是“闲杂人”，他一个搞理论研究的人纯粹是沾了毛主席的光，根本没有什么资格晋升少将．．．．．．还有那些唱歌的明星，咋能当将军？其实，这种观点是极其错误的狭隘的落后的。<br />
