<strong>附：朱厚泽书信散文――山之骨</strong><br />
<br />
　　钙，世代所珍。至于其人，山村野夫也。出身边陲，远离京华。无奈赤诚的良知乘时代的大潮将其卷入风暴漩涡。沉浮之间，身影偶现，时而入人眼目罢了。野气未消，钙性难移，但恐所剩无几矣。<br />
<br />
　　君不见，遮天蔽日的蒙蒙雨雾，吸附着千年郁积的瘴气与近代生活的污烟，早已把那山之骨溶蚀得满目疮痍。山岩挺立的轮廓，在晚霞的余晖中朦朦胧胧，昏昏糊糊，迷迷茫茫，已经难以辨认了。它正消失在黑暗之中……<br />
<br />
　　山之骨，它还会于晨曦中，重新披上彩霞，再现它的身影吗？<br />
<br />
　　是的，当那山之骨从溶蚀它的茫茫酸雨、地下潜流，从浩瀚的林莽深处、野草丛里，渗过泥沙与岩缝，历经艰辛和曲折，沉积、蒸腾、散发，扬弃了那污烟和瘴气之后，它必将会重新凝结出来。<br />
<br />
　　那洁白透明的钟乳，磷磷闪光的石花，巍峨的玉柱，雄奇的石林，神秘的溶洞……那不是新生的山之骨吗！那新生的山之骨，必将比它的母亲――被溶蚀的朴实无华的野性山岩，千般壮丽，万般诱人……<br />
<br />
　　对这一天，人们满怀希冀、信心和激情。但是，那只能存在于未来，我们难以触及的未来。它不会出现在明天，或明天的明天。<br />
