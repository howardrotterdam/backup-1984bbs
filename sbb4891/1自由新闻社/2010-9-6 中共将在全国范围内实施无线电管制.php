																											<div id="postmessage_816758" class="t_msgfont">中华人民共和国无线电管制规定<br />
<br />
2010-9-6 17:30:21 来源：中国政府网 <br />
<br />
　　中 华 人 民 共 和 国 国 务 院 令<br />
<br />
　　中华人民共和国中央军事委员会<br />
<br />
　　第579号<br />
<br />
　　现公布《中华人民共和国无线电管制规定》，自2010年11月1日起施行。<br />
<br />
　　国 务 院 总 理 温家宝<br />
<br />
　　中央军委主席 胡锦涛<br />
<br />
　　二○一○年八月三十一日<br />
<br />
　　中华人民共和国无线电管制规定<br />
<br />
　　第一条　为了保障无线电管制的有效实施，维护国家安全和社会公共利益，制定本规定。<br />
<br />
　　第二条　本规定所称无线电管制，是指在特定时间和特定区域内，依法采取限制或者禁止无线电台（站）、无线电发射设备和辐射无线电波的非无线电设备的使用，以及对特定的无线电频率实施技术阻断等措施，对无线电波的发射、辐射和传播实施的强制性管理。<br />
<br />
　　第三条　根据维护国家安全、保障国家重大任务、处置重大突发事件等需要，国家可以实施无线电管制。<br />
<br />
　　在全国范围内或者跨省、自治区、直辖市实施无线电管制，由国务院和中央军事委员会决定。<br />
<br />
　　在省、自治区、直辖市范围内实施无线电管制，由省、自治区、直辖市人民政府和相关军区决定，并报国务院和中央军事委员会备案。<br />
<br />
　　第四条　实施无线电管制，应当遵循科学筹划、合理实施的原则，最大限度地减轻无线电管制对国民经济和人民群众生产生活造成的影响。<br />
<br />
　　第五条　国家无线电管理机构和军队电磁频谱管理机构，应当根据无线电管制需要，会同国务院有关部门，制定全国范围的无线电管制预案，报国务院和中央军事委员会批准。<br />
<br />
　　省、自治区、直辖市无线电管理机构和军区电磁频谱管理机构，应当根据全国范围的无线电管制预案，会同省、自治区、直辖市人民政府有关部门，制定本区域的无线电管制预案，报省、自治区、直辖市人民政府和军区批准。<br />
<br />
　　第六条　决定实施无线电管制的机关应当在开始实施无线电管制10日前发布无线电管制命令，明确无线电管制的区域、对象、起止时间、频率范围以及其他有关要求。但是，紧急情况下需要立即实施无线电管制的除外。<br />
<br />
　　第七条　国务院和中央军事委员会决定在全国范围内或者跨省、自治区、直辖市实施无线电管制的，由国家无线电管理机构和军队电磁频谱管理机构会同国务院公安等有关部门组成无线电管制协调机构，负责无线电管制的组织、协调工作。<br />
<br />
　　在省、自治区、直辖市范围内实施无线电管制的，由省、自治区、直辖市无线电管理机构和军区电磁频谱管理机构会同公安等有关部门组成无线电管制协调机构，负责无线电管制的组织、协调工作。<br />
<br />
　　第八条　无线电管制协调机构应当根据无线电管制命令发布无线电管制指令。<br />
<br />
　　国家无线电管理机构和军队电磁频谱管理机构，省、自治区、直辖市无线电管理机构和军区电磁频谱管理机构，依照无线电管制指令，根据各自的管理职责，可以采取下列无线电管制措施：<br />
<br />
　　（一）对无线电台（站）、无线电发射设备和辐射无线电波的非无线电设备进行清查、检测；<br />
<br />
　　（二）对电磁环境进行监测，对无线电台（站）、无线电发射设备和辐射无线电波的非无线电设备的使用情况进行监督；<br />
<br />
　　（三）采取电磁干扰等技术阻断措施；<br />
<br />
　　（四）限制或者禁止无线电台（站）、无线电发射设备和辐射无线电波的非无线电设备的使用。<br />
<br />
　　第九条　实施无线电管制期间，无线电管制区域内拥有、使用或者管理无线电台（站）、无线电发射设备和辐射无线电波的非无线电设备的单位或者个人，应当服从无线电管制命令和无线电管制指令。<br />
<br />
　　第十条　实施无线电管制期间，有关地方人民政府，交通运输、铁路、广播电视、气象、渔业、通信、电力等部门和单位，军队、武装警察部队的有关单位，应当协助国家无线电管理机构和军队电磁频谱管理机构或者省、自治区、直辖市无线电管理机构和军区电磁频谱管理机构实施无线电管制。<br />
<br />
　　第十一条　无线电管制结束，决定实施无线电管制的机关应当及时发布无线电管制结束通告；无线电管制命令已经明确无线电管制终止时间的，可以不再发布无线电管制结束通告。<br />
<br />
　　第十二条　违反无线电管制命令和无线电管制指令的，由国家无线电管理机构或者省、自治区、直辖市无线电管理机构责令改正；拒不改正的，可以关闭、查封、暂扣或者拆除相关设备；情节严重的，吊销无线电台（站）执照和无线电频率使用许可证；违反治安管理规定的，由公安机关依法给予处罚。<br />
<br />
　　军队、武装警察部队的有关单位违反无线电管制命令和无线电管制指令的，由军队电磁频谱管理机构或者军区电磁频谱管理机构责令改正；情节严重的，依照中央军事委员会的有关规定，对直接负责的主管人员和其他直接责任人员给予处分。<br />
<br />
　　第十三条　本规定自2010年11月1日起施行。</div>

							
							
							
							
