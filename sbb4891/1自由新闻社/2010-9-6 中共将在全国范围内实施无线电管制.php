国际在线9月6日报道 近日，国务院和中央军委联合发布《中华人民共和国无线电管制规定》，为维护国家安全、保障国家重大任务、处置重大突发事件等需要，自2010年11月1日起，将在全国范围内或者跨省、自治区、直辖市实施无线电管制。<br />
<br />
《管制规定》指出，实施无线电管制期间，无线电管制区域内拥有、使用或者管理无线电台（站）、无线电发射设备和辐射无线电波的非无线电设备的单位或者个人，应当服从无线电管制命令和无线电管制指令。<br />
<br />
根据《管制规定》，违反无线电管制命令和无线电管制指令的，由国家无线电管理机构或者省、自治区、直辖市无线电管理机构责令改正；拒不改正的，可以关闭、查封、暂扣或者拆除相关设备；情节严重的，吊销无线电台（站）执照和无线电频率使用许可证；违反治安管理规定的，由公安机关依法给予处罚。<br />
<br />
军队、武装警察部队的有关单位违反无线电管制命令和无线电管制指令的，由军队电磁频谱管理机构或者军区电磁频谱管理机构责令改正；情节严重的，依照中央军事委员会的有关规定，对直接负责的主管人员和其他直接责任人员给予处分。<br />
