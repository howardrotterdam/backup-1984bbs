最后一课讲六四　暨大生寻牛老师<br />
<br />
<img src="http://thumbsnap.com/i/HzGyxJmT.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
深受学生尊重的牛永斌，至今未知去向。<br />
<br />
有不少香港学子求学入读的广州暨南大学珠海学院，发生震撼校园的政治事件。位于珠海的暨大珠海学院中文系讲师牛永斌，今年6月3日，即1989年六四事件21周年前夕，在上堂时公开讲述六四事件，令学生大受启蒙，但他事后辞职。有暨大学子最近在网上发起「寻找敬爱的牛老师」行动，希望得悉他的下落。<br />
<br />
有暨大学子在网上披露，牛永斌是暨大珠海学院乃至全暨大最受学生喜爱的老师之一。他主要讲授文学创作与鉴赏、当代中国概论、中国哲学史、艺术概论等课程。今年6月1日晚，他在课堂上透露已向校方递交辞呈，将不再授课。<br />
<br />
历3小时　中途无人离席<br />
<br />
6月3日晚，他为学生上课时突然离题，讲起21年前发生在北京天安门的那场「共和国最大的悲剧」，并播放有关片段。在长达三小时讲课里，他以诙谐的语言和深刻的思辩，打动学生，让学院最大的教室座无虚席，其间没有休息，没有学生中途离场。讲课结束时，讲台下爆发良久掌声。<br />
<br />
因时值六四21周年，牛永斌的行动不但震撼整个学院，也震撼总部位于广州的暨大，令校方大为震惊。次日（6月4日），被校方即叫他去谈话。<br />
<br />
后来牛永斌离开了校园，不知去向。校方则禁止学生公开议论有关事件向，珠海学院办公室亦拒绝回应外界有关事件查询。<br />
<br />
人民大学毕业　曾亲历89年学运<br />
<br />
<img src="http://thumbsnap.com/i/AyJYK9I6.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
1989年6月4日解放军进驻天安门广场，镇压示威学生。资料图片<br />
<br />
最近，有暨大学子在网上发起「寻找敬爱的牛老师」行动，并以隐晦文字记录事件过程，将事件与法国作家阿尔方斯・多德（Alphonse Daudet）名作《最后一课》（La Dernie`re Classe）作比较，表达对统治者精神奴役的不满，表达对正直、勇敢、睿智的牛永斌老师的怀念。<br />
<br />
据悉，牛永斌早年曾亲历1989年学运，后毕业于中国人民大学，是广告学硕士。他多年前受聘于暨大，在暨大珠海学院中文系任讲师。他讲授的课程题目虽枯燥，但却以渊博的知识和幽默的表述，吸引学生，成为暨大最受欢迎的教师之一。本报记者<br />
