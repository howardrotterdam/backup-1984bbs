													<h2>薄熙来对重庆市容大为光火 今年斥资1770亿折腾</h2>
						
						
																											<div id="postmessage_628243" class="t_msgfont">重庆1770亿“整容”<br />
<br />
“现在到处都在弄，要整出一个新重庆。”5月18日，冯伟开车带外地朋友逛重庆，对于到处可见的道路修整、防护栏上漆、住宅楼撤换外立面等市政美容工程，冯对朋友如此解释。<br />
<br />
冯伟是重庆一家广告公司的老板，但由于主营的两个户外广告牌市容整顿中被拆除，他不得不解散了公司。<br />
<br />
始于3月28日的这场轰轰烈烈的市容环境综合整治运动有着远大的目标。按其主推者、重庆市委书记薄熙来在5月12日考察主城区城建改造项目时的说法，“力争三五年，使重庆有个大变化，在城市面貌上要和‘直辖’称谓名副其实，和京津沪比肩而立。”<br />
<br />
随着近年经济发展及城市化提速，重庆希望迅速扫去积年的尘埃，以步入现代化大都市行列。<br />
<br />
但这并不是一件简单的事。<br />
<br />
整治风暴<br />
<br />
这场行动首先给户外广告牌造成震动。3月28日起，重庆主城出现大批施工队伍，集中开始了户外广告拆除。<br />
<br />
拆除行动非常迅速。市政委用“夜以继日”来形容操作人员的工作状态，而“广告商很配合，没有强制拆除行为”。<br />
<br />
据市政委统计，从3月28日到4月23日，短短20多天时间内，已拆除有碍市容的户外广告牌2.38万块，总面积55.8万平方米，占到整个重庆户外广告的75%。<br />
<br />
广告牌只是市容综合治理的内容之一。根据市政府在4月提出的要求，市容环境半个月要见成效，广告整治一个月见成效，占道摊点的清除三个月见成效。<br />
<br />
4月24日 重庆市政委召开全系统干部大会，主任王元楷表示，城市环境综合整治工作进入了倒计时，要苦干实干100天，到8月10日，使城市环境迎来一个全新的改变。<br />
<br />
王元楷与在座干部“约法三章”：其中之一是工作要求做到高效率、快节奏、满负荷，“白加黑”、“5+2”。市政委为此还确立了主城各区县市容整顿的考核制度，并派员进行明查暗访。<br />
<br />
整个综合环境治理，以难以想象的速度，席卷重庆大街小巷。<br />
<br />
在渝北区57公里长的金开大道，在一个多月时间，栽了2万多棵银杏和香樟树。在南岸区，外观丑陋的酒店、写字楼外立面都在进行整治。而渝中区则提出，今年全年要改造100万平方米的楼房外立面。<br />
<br />
据主城的沙坪区公布的数据，到目前为止，全区的24个街镇，无论是主干道或是背街小巷都全部进行清理整治，此次整治过程中共清理暴露垃圾3525吨，整治亭、棚、伞、摊9892个，清除&quot;牛皮癣&quot;12万余处，冲洗道路路面10764平方米。<br />
<br />
一场战役<br />
<br />
这场城市改造运动由薄熙来发起。之前，他在大连任市委书记期间，已在这个领域获得了很好的成效。<br />
<br />
在2009年12月底召开的全市经济工作会议上，薄熙来便强调，从2010年开始，重庆需要用较大的力度去做城市建设，为改变重庆面貌发起了一场“战役”：“前2年政府做了很多铺垫，明年开始要大干，3年‘大变样’，10年‘脱胎换骨’。”<br />
<br />
但随后的进展并未达到他的要求。薄熙来在今年3月下旬召集市政及主城区负责人开会。据市政府一位官员透露，薄熙来在会上发了火，“我对重庆的市容不满意，真的不满意，就是不满意。”<br />
<br />
在此情形下，3月28日，重庆立即召开主城区城市建设管理工作会议，按市政府要求，由市城乡建委、市市政委、市园林局、市国土房管局、市工商局五部门牵头，决定“加大火力”推进城市环境综合整治工作。<br />
<br />
不久，市政委一把手换人。4月14日上午，重庆市人大常委会决议任命原工商局局长王元楷出任市政委主任。<br />
<br />
这一切，使很多官员意识到，“必须下猛药进行城市环境整治”。<br />
<br />
根据重庆市政府的规划，在实施市容整治同时，要利用今、明两年的时间，对主城区历史形成的27条老街区、133公里主干道，实施环境综合改造。这些改造，预计今年将投入1770亿元。<br />
<br />
“要通过改造，让城市给人刮目相看的视觉效果。”黄奇帆说。<br />
<br />
寻求长效机制<br />
<br />
重庆这番城市改造，受到市民广泛的好评。但有也一些异议之声，有人认为这是搞“形象工程”。<br />
<br />
“重庆这些年城市建设总体提升很大，但也有很多落后的盲点，必须通过扫荡式的整治，使城市整体水平得到提升，这是重庆城市文明程度提升必须要做的事。”重庆迈德城市研究院院长赵宜胜代表了支持者的观点。<br />
<br />
薄熙来则在12日解释说，一个城市自身的形象，是千家万户的生活环境，并不是所谓的“形象工程”城，市建设即是民生问题，也是经济问题，发展问题。<br />
<br />
薄熙来表示，高水平的城市建设对经济的推动作用将无可限量，所以重庆当前必须有针对性地改善投资环境，以吸引国际优秀的公司，这是推动重庆高速发展的重要环节。<br />
<br />
工商大学副校长廖元和对此持赞同观点。他认为，现在构成一个城市核心竞争力的，已不是政策优惠与否的问题，而是城市投资环境优质与否。<br />
<br />
“政策优势会逐渐消失，并趋于公平，而投资环境，尤其城市环境，将成为吸引投资的突出竞争力，最终达到城市不断增值和可持续发展的目的，以后其它城市都会往这个方向发展。”廖元和说。<br />
<br />
但这些宏大意义背后，亲临其间的一些市民，有他们真实的烦恼。<br />
<br />
例如有市民觉得生活开始不方便。“现在猫市、狗市都没了，这是古老的文化，怎么就一刀就切了呢。”大渡口区一位养狗多年的市民发现，这些市场都被关闭了。<br />
<br />
反映最多的是主城报刊亭。一位汪姓市民说，他有一天为买报纸，走了一个小时，都没找到一个报刊亭。这些报刊亭因为管理存在不规范问题而被大批量拆除。<br />
<br />
一些重庆人喜欢的大排档也大多被取缔。一位市民很遗憾地说，薄书记不吃大排档、喝夜啤酒，体会不到那种乐趣。<br />
<br />
一位被没收了烧烤架的烧烤摊主抱怨说，他是下岗工人，得靠这个挣钱。<br />
<br />
重庆主城区一位街道办事处负责人对本报称，这是很现实的问题，报刊亭、烧烤、街边游摊，大都是下岗、待业的人员所办，强制性取缔后怎么解决这些人的就业问题呢？<br />
<br />
“如果只堵不疏，整顿之后就会死灰复燃，必须要有长效的办法和机制。”这位负责人称。(21世纪经济报道)</div>

							
							
							
							
