																											<div id="postmessage_165692" class="t_msgfont">2009年07月17日09:14&nbsp;&nbsp;来源：人民网-《环球时报》<br />
<a href="http://politics.people.com.cn/GB/1026/9671341.html" target="_blank">http://politics.people.com.cn/GB/1026/9671341.html</a><br />
<br />
在乌鲁木齐“7.5”事件发生后，有学者建议国家应制定专门针对骚乱事件的法律　。对此，环球网网友也表示赞成中国出台“反骚乱法”，坚决依法惩治暴力分子。 <br />
<br />
　　近日，上海国际问题研究院国际组织与国际法研究中心学者薛磊在《环球时报》上撰文表示，乌鲁木齐“7.5”　事件的处理也暴露出现有法律和制度中的一些缺陷。他在文中表示，我国目前并没有专门的《紧急状态法》，动用武警部队乃至解放军部队执行维护安全保卫任务确实存在一定的法律空白。从实践来看，仅仅依靠地方政府的紧急预案抑或恢复奥运期间的安保措施，是不能迅速以雷霆之力打消犯罪分子的嚣张气焰的。他认为，国　家推出“反骚乱法”已成为当务之急。 <br />
<br />
　　“中国是否应尽快推出反骚乱法？”针对这一话题，环球网网友给予了热烈的讨论。截至16日21时，有高达　10111位网友投票表示，赞成推出反骚乱法的提议，支持率为90.3％。 <br />
<br />
　　网友认为，推出反骚乱法的目的是从法律层面上应对社会安全事件和恐怖袭击，从而更好的维护中国社会的稳定。一位环球网网友说，目前中国的经济还处在发展阶段，尚须巩固。国内的安定、和谐才能使经济发达，国民富有。因此，“暴力事件必须坚决打击”，而“健全的法律制度才是根本保证”。 <br />
<br />
　　还有网友认为，反骚乱法实际上也可被视作一个“预防机制”。网友认为：“正如专家所说，单靠一些地方政　府的效令已经没法迅速打击这些暴力分子的嚣张气焰了。建立反骚乱法，意味着中国打击分裂势力的力度正在　加大，正可以给那些分裂分子以震慑！” <br />
<br />
　　多数网友在留言中提到，对实施暴力事件、破坏社会稳定的犯罪分子须坚决依法处置，绝不姑息。一位网友就说，从“7.5”事件来看，这不是什么民族问题、宗教问题，这是一小撮分裂势力造成的骚乱，这是犯罪行为，是恐怖行径。这位网友表示，“支持建立反骚乱法”，“一切危害公共安全、意图制造社会骚乱和分裂的行为　，都应该受到法律的严惩”。</div>

							
							
							
							
