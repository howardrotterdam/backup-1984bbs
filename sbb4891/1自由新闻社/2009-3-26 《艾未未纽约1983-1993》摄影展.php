<br />
　　艾未未的<br />
<br />
　　这些照片，是我在纽约的1983年至回到北京的1994年拍摄的，前后十年的时间。那时我没有什么事情可做，总是闲着，随手拍了一些遇见的事，去过的地方，熟悉的人，我的周围的住地、街道和城市，打发每天的闲散时间。 <br />
<br />
　　一晃20年过去，纽约已经不是当年的纽约，下东城的东村的面貌全变，照片中的很多人已经不在世，当年常在一起的人，也早已面貌全非，不再亲近往来，不再是朋友，各奔东西。这里所涉及的人,大多并不知道有这些照片存在。今天，平淡地面对过去，它们都不再真实。既确定而又不真实，毕竟任何的现实都是变化中的事实，是漫长的时间中不确定的片刻，现在总是覆盖着过去，未来也不将理会今天。 <br />
<br />
　　我陆续整理出一些照片，不是为了记忆，它不重要，那段走过来的路，不必是那一条，也可能走向另外一方。那些曾认识的人，也大可不必认识，人性本身孤独，一起时或许快乐温暖，之后想起来难免可疑。我整理它们，是因为这些图像本身始终是一个真实的物质存在，那些游离在相纸上的化学物质固定一处，不再游离，在黑暗中形成了秩序。它们在我这里，它所涉及的人和事件，包括我的过去，早已不重要了。 <br />
<br />
　　生活在过去的50年中，很像是一片落叶，没有目的没有方向，但最终还是会落在某个角落。图片出现的方式和秩序也会是如此，杂乱无章的，有些思路好像只有混乱时才可以清晰。 <br />
<br />
　　今天我依然手不离相机，习惯性地按下快门。应该说明的是，我既无兴趣于摄影，也不太在意所拍之物，它始终是与我的存在并行的另一个现实，每当再看到这些照片，总是发现陌生感大于熟悉感。<br />
