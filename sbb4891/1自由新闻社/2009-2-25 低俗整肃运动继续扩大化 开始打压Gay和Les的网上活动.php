													<h2>《南方周末》:同性恋网站为何遭黑客攻击？</h2>
						
						
																											<div id="postmessage_40161" class="t_msgfont">去年8月到10月，黑龙江、辽宁、北京、河北、天津、山东、江苏、浙江和重庆等地男同性恋网站相继遭到黑客攻击，数百万网友的资料被窃取。令人意外的是，这样大面积的网络安全事件，各地公安部门接到报案后并没有受理。<br />
<br />
经过多家受害者网站数月的“追查”，其中一名黑客的踪迹已被查明。“爱心天空公益网站”的负责人马龙委托律师，决定通过法律程序维权。在夹缝中生存的同性恋网站权益遭受侵犯后，多选择忍气吞声，马龙成为敢于站出来的第一人。<br />
<br />
十余男同性恋网站同时被袭<br />
<br />
去年8月6日，“辽宁同志”（下称辽同）的聊天室忽然出现了异常，一个自称“中国第一黑客”的人称，“请大家访问<a href="http://www.ln1069.com" target="_blank">www.ln1069.com</a>网站，此聊天室正式作废！”随即聊天室再也无法登陆。<br />
<br />
该网站也自称“辽宁同志”（下称新辽同）。“人一下子都跑到他那里去了。”辽同负责人大刚介绍说，他们的网站一次一次地遭受攻击，一天攻击达10次以上。在接下来的一个月里，辽同无论把服务器选在哪里，都会遭到攻击。<br />
<br />
在一个半月的时间里，辽同换了服务器不下20次。“我们每次只敢租一个月，没几天又死机了。钱损失了一万多。”辽同另一位负责人风云说。<br />
<br />
与此同时，男同性恋网站“爱心天空公益网站”（下称爱心天空）聊天室也在去年9月4日遭到了攻击，网友一登陆就弹出一个窗口：“本网站正式关闭，请登陆<a href="http://www.h1069.com" target="_blank">www.h1069.com</a>网站”。 黑客利用聊天室的漏洞盗窃数据后，对硬盘进行格式化,使网站数据丢失。<br />
<br />
“网友一进聊天室就被踢出来。”“爱心天空”的负责人马龙告诉南方周末，黑客利用木马和程序漏洞，下载网站数据，并用这些数据成立了一个新的“爱心天空”。“还说爱心天空已经被它们收购，我们这个网站是假冒的”。马龙说。<br />
<br />
网站被攻击之时，马龙“心里很难受”，“因为我的整个同志生活的信心都来自网络”。<br />
<br />
当年8月23日，另一家同性恋网站“天津酷网”也遭到了攻击，服务器被格式化，建站9年来的数据丢失，损失万元左右。<br />
<br />
“黑客明显是在挑起事端，假冒我们的名义攻击‘天津酷网’。”风云说。<br />
<br />
在去年8至9月间，北京、河北、天津、山东、江苏、浙江和重庆等地的同性恋网站都遭到了攻击，聊天室遭到了重创。<br />
<br />
同性恋网站因其身份的特殊性，历来是黑客攻击的重灾区，但这次大规模的攻击到底是为什么？<br />
<br />
黑客要求“给钱消灾”<br />
<br />
去年10月4日，北京爱知行研究所在沈阳召开了“艾滋病、性安全和法律工作会议”。马龙也受邀参加。议题本来事涉“同志”群体“健康干预”问题，因黑客攻击网站事件，于是会期临时增加一天，专门讨论网站安全问题。<br />
<br />
参会期间，马龙依然手捧着笔记本电脑，“与黑客作战”。不断地刷屏点击。“他当时的架势，就像在一个没有硝烟的战场上厮杀死拼。”一位与会人士说。<br />
<br />
马龙回忆说，黑客攻击的高峰之时，攻击者甚至通过网路传输来了自己的QQ号和照片。<br />
<br />
照片中人年约二十五岁左右，面容清秀，但照片的真伪无从查证。<br />
<br />
新辽同和新爱心天空的域名相似，黑客攻击手法也很类似，经过调查，北京爱知行研究所所长万延海等人发现两个域名的注册人同为“ChenTianqiao”。8月底，新辽同加入国内同性恋网站“淡蓝联盟”，签协议时用的名字叫做“陈天桥”。<br />
<br />
与此同时，“ChenTianqiao”创办两个网站所用的QQ号的信息都指向一个叫做“九天科技”的网站或公司。九天科技对外出租聊天室业务，业务用手机号和“陈天桥”一致。<br />
<br />
就在爱知行拿到了充足的证据证明攻击行为和九天科技有关时，发生了戏剧性的一幕。“陈天桥”找到了大刚，说要把新辽同的域名卖给他。经过一番讨价还价，双方以6000元成交。<br />
<br />
就此，新老辽同在进行了一番网络争夺战后，退出了战局。9月17日，“陈天桥”到指定的地方取走了钱。辽同聊天室当天的人气就超过了600人。全国各地的同性恋网站恢复了稳定。现在<a href="http://www.ln1069.com" target="_blank">www.ln1069.com</a>和 <a href="http://www.h1069.com" target="_blank">www.h1069.com</a>两个链接都已经打不开。<br />
<br />
万延海回忆，马龙向他们求助后，马上意识到这不是来自官方的力量，攻击者不是为传道而来，是为利益而来。<br />
<br />
黑客的攻击手法很相似：首先攻击服务器导致网站瘫痪；盗取数据后建立新网站；宣布原有网站关闭，将网民拉入新网站。这样的做法显然是窃取这些男同性恋网站数年来的成果。<br />
<br />
新辽同成立后，其创办人曾找到辽宁另一个同性恋网站“男孩LB”，要求加入他们的网站联盟，成为其旗下网站。双方未达成协议，“男孩LB”的服务器也遭到了攻击。</div>

							
							
							
							
