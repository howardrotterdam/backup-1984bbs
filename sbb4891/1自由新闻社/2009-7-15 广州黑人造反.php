痴迷的人&nbsp;&nbsp;<br />
<br />
坦言堂<br />
<br />
自从世界流行歌坛天王巨星麦克杰逊猝死的消息传出后，几天下来，报章、杂志、电视台、电台等媒体，就都铺天盖地的报道他的身世，他的成长过程，他的成名，他缠身的许多疾病，他的毒瘾，他的绯闻，他的婚姻，他的演唱会，他的……还有，许许多多的歌迷因他的猝死而大哭得如丧考妣，举行追悼会……这一切，真令人眼花缭乱。啊！这世界痴迷的人太多了。<br />
<br />
几十年来，我不曾听过麦克杰逊的摇滚音乐（也许是我不会摇滚的缘故）。但我见过他跳舞唱歌的照片。他的舞跳得乱七八糟，唱歌时脸上的表情十分痛苦。我很反感。跳舞唱歌怎会是这样痛苦的事呢？<br />
<br />
此外，我知道他原是黑人，但他很讨厌做黑人，所以他花很多钱，很长时间，把自己全身漂白，还为象征黑人的厚嘴唇施手术削薄。为使自己更不像黑人，他去把自己鼻子整容得很尖，更像白人。终于，他变成了一个不是自己族类的漂白的黑人。<br />
<br />
他洗澡时必须用一种法国矿泉水，他睡觉时必须罩上氧气口罩。他花很多钱建造自己的“梦幻庄园”以享乐。每当他要去迪士尼乐园时，他先用大笔的钱包下园里所有的娱乐设施，然后一个人去独乐乐。<br />
<br />
他两次的婚姻都以失败收场。可是他与世界上的名女人，却又发展出一场场畸形的恋情。其中有年龄比他大了很多的，有波霸、有电影明星、有守寡再醮的美国前第一夫人……他的爱情生活，就像他跳舞唱歌一样，乱七八糟。<br />
<br />
后来他又卷进了娈童案，他说那是他小时受父亲虐待而导致的。乐圣贝多芬小时也被父亲虐待过，夜间不许他睡，要他一直练琴。终于他四岁上台演奏钢琴，比麦克杰逊五岁上台演唱，还早了一年。<br />
<br />
我们难道不觉得麦克杰逊是个不正常的人吗？此人的猝死，竟受到世界上数以亿计的人为他疯狂大哭。这世界痴迷的人太多了，他们都在崇拜一个不正常的人。<br />
