																											<div id="postmessage_163783" class="t_msgfont">广州黑人达20万 揭密鲜为人知的故事　<br />
　　<br />
　　<br />
　　2007年 12月 13日 19:38 盐田网<br />
　　<br />
　　<br />
　　Felly在他的办公室里接受记者采访。<br />
　　<br />
　　<br />
　　很多黑人在中国组建自己的家庭<br />
　　<br />
　　 深圳新闻网讯 (记者柯学东、杜安娜)近年来，不少广州市民发现，身边来自非洲的黑人越来越多。本来泛指外国人的“鬼佬”，几乎成了黑人独享的代名词――他们就住楼上楼下，会跟我们一起去菜市场买菜、挤公共汽车、“抢”出租车，甚至会向年轻的女市民发出约会邀请。<br />
　　<br />
　　 据广州社科院城市管理研究所所长黄石鼎透露，目前在广州常住（6个月以上）的外国人数已达5万，其中可统计的非洲人就有2万多。但这个数据显然不包括数量不详的隐居群落。据统计，目前在广州的黑人每年以30%~40%的速度递增，有人估计总数已达20万之巨。<br />
　　<br />
　　 这是广州步向国际化的一个标志。人们知道，数量庞大的非洲黑人促进了中非贸易发展，在一定程度上加速了广东产业升级。然而，黑人“版图”中很多鲜为人知的故事也正在发生。<br />
　　<br />
　　 庞大的黑人“版图”分布如何？他们每天在过怎样的生活？专家直指，对黑人“部落”管理难度超出很多人的想象。<br />
　　<br />
　　 近两周，本报记者在广州洪桥、永平街等地进行了深入的调查，并请教有关专家学者，揭开目前在中国越来越活跃的“黑人部落”面纱。<br />
　　<br />
　　 在广州，越秀区以洪桥为中心，黑人群落形成了经济和劳务的两种依附关系；而在一些城乡接合处，有着人数众多的非洲“黑工”，他们依靠每月多则千余元，少则一两百元的收入生活。<br />
　　<br />
　　他们群居在一个特定的区域，为躲避管理几天不出门，靠方便面和矿泉水度日；他们有着自己特有的信息传播途径，能在20分钟内把警察查护照的消息发布到数以千计的群体；男女比例的严重失调，黑人强奸案也直线上升；他们还诈骗、抢劫、贩毒……却从不想回到自己的家乡。<br />
<br />
做“倒爷”：主攻“低端”产品<br />
　　<br />
　　 Felly来自刚果，是活跃在洪桥天秀大厦的“国际倒爷”之一，名片上写的是刚果驻广州一家贸易公司的首席代表，而他在跟记者介绍自己时，却说自己是公司的老板。显然，Felly想把自己跟那些日夜流荡在洪桥“h工”的黑人劳工区别开来。<br />
　　<br />
　　 Felly的办公室在天秀大厦B座15层，一个景观不错的位置。20多平方米的客厅陈列着各色商品样本：摩托车、电视机、录音机和音箱。聊天间隙，他打开电脑，放了首最喜欢的中文歌《两只蝴蝶》。<br />
　　<br />
　　 才28岁，Felly已经有10多年的经商史，到广州已四年，中文说得很好，也能听懂粤语。他有着商人的精明和谨慎，在接受记者采访时，Felly只谈自己的事，对其他则缄口不言，比如非法入境的非洲人。<br />
　　<br />
　　 像很多非洲“倒爷”和劳工一样，Felly一开始去了香港的“重庆大厦”――一个被称为亚洲最具国际化特色的大楼，但香港的东西很贵，待了近6个月来到广州。<br />
　　<br />
　　 Felly是个“生意精”，以前曾在迪拜做生意。后来看到有人大包小裹地从中国携带商品回国出售，这样的一次淘金之旅，除去机票、食宿和当地海关不同名目的“罚款”，净赚上千美元没问题。刺激之下，Felly也来到了中国。<br />
　　<br />
　　 现在Felly的公司的主要业务是摩托车、电子、服装。记者发现，那些样品中，除了摩托车外，收音机、电视机基本上都是旧货，是中国人上个世纪八九十年代用的产品，其中录音机还是在中国市面上几乎绝迹了的卡带机。<br />
　　<br />
　　 虽然这些产品在中国已经过时，但并不影响他在刚果的销路和利润。“在这边1000元的进货，在我们那边可以卖到2000~3000元。而且可以直接从广州通过轮船运输。”Felly说。<br />
　　<br />
　　 记者在调查中发现，Felly的生意是洪桥非洲黑人采购的样本――主要是低端产品。就天秀大厦而言，大多数商铺经营的是纺织品，而且质地非常普通。偶然有些电子产品，也是二手货或者过时产品。<br />
　　<br />
　　曾长期调查非洲黑人群落的广州社科院城市管理研究所所长黄石鼎告诉记者，广东作为世界制造业中心，过去十几年，产能是过剩了，很多公司库压了大量的旧货，被洪桥下的天秀大厦、陶瓷大厦、秀山楼等中非贸易集散地很好“消化”了，就像当年中国一些不法商人通过走私渠道从欧美、日本等市场大量进口二手车是一回事。但就广东而言，这对产业升级和促进中非贸易是很有帮助的。<br />
<br />
　　促贸易：非洲到处是“中国制造”<br />
　　<br />
　　 像Felly那样的刚果商人在中国有不少，据官方透露，目前中国使馆每个月向刚果(金)发放至少800个签证。目前在中国的刚果(金)公民约有2000到3000人，其中大多数居住在广州。<br />
　　<br />
　　现在，Felly说自己已经完全习惯了在广州的生活，他的公司还雇用了几个中国人。“广东话我能听懂，也习惯了中国的饮食，特别是火锅。”提到火锅他很兴奋。“广州的天气热的时候比我们热，冷的时候又比我们冷。但比起北方，要好很多，所以我们去北京做生意的人不多。”<br />
　　<br />
　　 说起广州，Felly一个劲地说，“一个好地方”，“可以买任何东西，去任何地方都很方便”。而对于祖国刚果，他则有些伤感：“我很想家，但是我是男人，要赚钱。<br />
　　<br />
　　 Felly说自己很想融入中国的社会，现在他的生活圈子很窄，这也是非洲人群落的共同特点。“在广州的朋友并不多，除了刚果来的外，中国的朋友主要是大学生，和他们比较好交流。”在广州，有不少非洲人跟市民有着很不错的交流，比如现在广州就有个完全由非洲人组成的业余足球队，他们定期会跟中国一些业余足球队进行比赛和交流。Felly说他本想参加足球队，但“生意很忙”，所以不得不放弃。<br />
　　<br />
　　 这两年，他并不认同黑人同胞的某些行径：“我不像其他人，随便玩一玩。我希望找到一个我爱的人，与她结婚。”他说到自己曾经有一个读大学的中国女友，不过因为一些原因，以分手告终。<br />
　　<br />
　　 Felly今年的生意并不好做，他用“麻烦”来形容：“人民币升值了，很多朋友都去寻找新的进货渠道。因为广州的生活很贵，也有些朋友去义乌寻找机会了。另外，在东非中国人也在那里建立了自由贸易城，他们提供的商品比我这里进口的还要便宜，所以竞争激烈了。”他说，自己还是在广州待一段时间，看看行情如何。<br />
　　<br />
　　 在Felly看来，在洪桥这个地方，像他这样的贸易商，对中非的贸易是有很大的促进作用的，“现在非洲到处是‘中国制造’的商品，都是我们这些人买过去的。”<br />
<br />
　　广州有个“黑人区”<br />
　　这是小北路。大约从5年前开始。一批一批的南亚、非洲、中东商人在此住脚扎根。到今天已经蔚为大观。<br />
　　<br />
　　秀大厦里的办公室、批发商场的摊档，2/3是这些外国商人在做“BOSS”。以小北立交为中心，10公里半径。北到元下田（白云山最北的管理区）、南到江南西、西到增嵯路、东到五山.活动着约五万黑肤卷发，讲阿拉伯语、英语、的 “黑色族群”，其中长住人口已经接近2万。 这是打好货准备去发运的青年。<br />
　　<br />
　　<br />
　　<br />
　　　　“黑人区”、“黑色族群”仅是称谓而已，没有半点歧视在里面。这个“黑色族群”在广州，已经慢慢扎根了。 他们有些甚至会讲广州话！ 瞧，公共汽车是他们的主要交通工具。</div>

							
							
							
							
