																											<div id="postmessage_74664" class="t_msgfont">南方都市报4月15日社论：以人权的名义公开震亡者大名单<br />
 <br />
　　国务院新闻办公室13日发表了《国家人权行动计划（2009-2010）》，承诺保障公民权利与政治权利，包括人身权利、被羁押者的权利、获得公正审判的权利、宗教信仰自由、知情权、参与权、表达权、监督权的保障。同时保障媒体的采访权、批评权、评论权、发表权。其中还涉及保障汶川大地震灾后重建中的人权，为尊重遇难者，承诺对地震中遇难和失踪者登记造册并予以公布。<br />
　　在四川强震发生后的300多天里，重建过程中传出过负面的行政事例，给全国民众造成了困扰和担忧。普遍的疑虑之下，政府以何种态度对待震区民众的人权，始终成为不曾转移的关切点。生者何所依，死者何所道，国家首次就此公开许诺，终于将尊重人权补充为震区重建的核心原则。虽然只有时间表，尚缺路线图，但在人权的旗帜下，提高了迈向正确方向的可能性。<br />
　　可也要看到，就在即将迎来大地震周年祭祀之际，仍然没有一份完整的名单，尽数将罹难同胞和失踪者收录在内。震恸尚且不绝，无名伤逝尤其悲哀。姓甚名谁绝非无关紧要，它们的一撇一捺组成人权的基本笔画。因为每一个名字都是生命的象征，像流星指示星空那样，都代表着曾经热烈生长又迅即断裂的生活。任由亡者的姓名埋没，犹如罔顾生民的处境，人权也就无所托付。<br />
　　将地震受害者与人权画上等号，不是为了将姓名神圣化，而是要强调人权质朴且直击人心的一面。归结到汶川地震，遇难者作为人存在过，也当以人的礼遇接受虔诚的祭奠，循着姓名的线索，才能寄上真实的祈祷。最坏的情形是，若不得不向无名纪念碑低头致哀，或让国民去祭拜大而化之的罹难者数字，那将是谁也不能承受的耻辱，必定会在历史上写下难以消除的羞愧二字。<br />
　　以国家人权计划为衡量标准，任何将遇难者和失踪者名单作为秘密的举动都是不可理喻的，更违背这一人权行动计划所象征的民众要求。人权计划出台后，为了减少重复劳动，震区的地方政府，应公开已经掌握的名单，并彻查遗漏的姓名。而这些人权名单属于所有民众，可以被自由查阅、获取和传播，也同样可以被自由地查漏补缺。<br />
　　尽管目前仍然残缺不全，但震亡者与失踪者名录是四川地震留给全体国民的遗产。政府部门曾经强调要对遇难人员进行详细核实核查，收集姓名、籍贯、遇难地点等基本信息，因此会是一个非常复杂的程序，必须分期分批公布，核实一批公布一批。应该承认，核实遇难者名单是一项庞大的工程，甚至会涉及遇难经验教训总结问题，因此在四川地震一周年祭将至之时，公众对于尽快公布遇难者包括遇难学生名单怀有热切的期待，因为这也是灾区重建工程的一部分，是我们公民社会自我治愈的一个程序。<br />
　　事实上，最近一段时间以来，已有一些志愿者在四川自发地整理地震中遇难的学生名单，他们通过网络搜索引擎谷歌、百度提供的无数链接以及公开新闻报道，以电话或亲自拜会当地相关政府部门、遇难者亲属等咨询方式，搜集和整理相关的信息。这既是对政府部门主动行使职守去厘清谜团的压力，更是为落实人权行动计划相关承诺提供巨大动力。尊重接纳社会组织或志愿者团体介入名单的搜集、整理和发布等，而不是以狐疑或排斥的态度看待这些公民以及团队的自发努力，将是落实公民权利法案的最好见证。<br />
　　强调建立并公开震亡者名单，详实登记他们的性别、年龄、籍贯、身份等，并非为了争取什么权利，只是为了抵制无时不在的遗忘，实现更庄重的纪念。名字体现人权，名字就是悲欢离合，展现往昔的生活片断。由此，呈现他们生前的笑脸，察见那些已然破碎在五月的梦想，抚慰那些永远不能愈合的伤痛。在名单的帮助下，地震就不可能真的夺走那些同胞，因为他们留下了一世为人的凭据。<br />
