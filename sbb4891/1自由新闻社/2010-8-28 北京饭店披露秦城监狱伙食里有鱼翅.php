																											<div id="postmessage_805220" class="t_msgfont">何殿奎：亲历“中国第一监狱”监管岁月<br />
总第462期&nbsp;&nbsp;2010年04月01日&nbsp;&nbsp;往事&nbsp;&nbsp;本刊全部文章 来源：《中国新闻周刊》&nbsp;&nbsp;作者: 字号：T|T<br />
<a href="http://magazine.inewsweek.cn/magazine/recommend-275.html" target="_blank">http://magazine.inewsweek.cn/magazine/recommend-275.html</a><br />
何殿奎：亲历秦城监狱监管岁月<br />
“中国第一监狱”秦城监狱，是1950年代苏联援助新中国157项经济与国防建设的工程之一。高级别、高规格、高待遇，使得秦城监狱成为特殊监管的别名。而一部顺序记录下来的、发生在秦城监狱的故事，则成为历史的特殊佐证。<br />
78岁的何殿奎住在北京市金融街旁一个老旧而安静的小区里。这里跟金融街近在咫尺，却完全是两个世界。<br />
何殿奎离休前是秦城监狱监管处的处长，在秦城监狱及其前身功德林监狱担任监管员近40年。几乎没有第二个人能像他一样，阅尽了中国政坛高层人物的起伏面孔。<br />
“我有这么个思想。这些老干部怎么能犯了罪呢？这我有怀疑。为什么产生怀疑呢？好多案子引起我的怀疑。‘青岛案’那么多干部被抓，都错了；‘广东案’更大，一二百人，都错了；五几年，内部肃反，弄来弄去又错了一批。对这些老干部，必须要保护好，服务好。把自己的工作做好就行了。”何殿奎告诉《中国新闻周刊》记者。<br />
（编者注：“青岛案”即1955年青岛市委书记王少庸等人的“反革命内奸嫌疑”案；“广东案”即1951年广州市公安局副局长陈泊等人的“特务”案，皆系重大冤假错案，已平反）<br />
近来，他跟公安部的另一位离休干部汪春耀开始合写一些回忆文章。汪以前常到秦城监狱提审犯人，跟他相识。用汪春耀的话说：他们这些小人物，脑子里装了太多大人物的故事。<br />
何殿奎的记忆力只能用“惊人”二字来形容。一串串的人名、数字，从他嘴里出来，不用思考，不用回忆。<br />
　　<br />
独立卫生间、地毯和鱼翅<br />
何殿奎永远记得这个日子：1960年3月15日，秦城监狱落成的第一天。从那天起，他就在那里工作，直到1992年离休才离开。<br />
苏联援建的秦城监狱，由4栋3层青砖小楼组成，编号分别为201、202、203、204。后来参与审判“四人帮”的法官王文正在《以共和国名义判决》中写道，其设施和条件，“远远超过当时中国普通人家的居室”。那是1980年，作为预审员的他就住在秦城监狱的监房里办公。每间监房都有独立卫生间，带脚踏式冲水的抽水马桶。<br />
王文正可能不知道，他住的仅仅是条件最一般的201监区。秦城的四座小楼，内部结构都不一样。204监区，待遇更非一般人所能想象。<br />
204的监房约20多平方米，铺着地毯，床是沙发床。伙食标准是按部长级待遇，到东华门“高干供应点”采购。早餐有牛奶，午晚餐是两菜一汤，饭后有一个苹果。苹果是刚从冷库里拉来的，放在稻糠里保鲜，拉来时那苹果都冒着气儿。还给他们发固体饮料，一盒12块，一块能沏一杯柠檬茶。方糖分白色和咖啡色的两种。每天如此，即便在困难时期都一样。<br />
给他们做饭的则是专门从北京饭店调来的乙级厨师刘家雄。就是在那里，何殿奎第一次见识了鱼翅。他以为那是粉丝，刘家雄告诉他，是鱼翅，就是鲨鱼的鳍。只有刘家雄一个人会发海参、鱼翅这些东西。<br />
每天，都由何殿奎给他们送饭。每人一个四层的饭盒，分别装米饭、两个菜和一个汤。冬天用棉罩保温。每个饭盒的颜色都不同，以示区分。一共15份。<br />
这些重量级犯人是从功德林监狱的“特监”迁来的。1956年7月，功德林设立了一个专管高级干部的“特监”区，把这些人从不同监区集中起来管理，何殿奎是监管员。“特监”一共8个人：原来的中央组织部部长饶漱石、上海市副市长潘汉年、上海市公安局局长扬帆、从事特情工作的袁殊、外贸部副部长徐雪寒、广州市公安局副局长陈泊、《人民文学》编委胡风和一位公安部的局级干部。<br />
秦城监狱建好后，这些人全部迁入204监区，青岛市委书记王少庸等几位“青岛案”主犯也从别处迁来，一共15位。<br />
