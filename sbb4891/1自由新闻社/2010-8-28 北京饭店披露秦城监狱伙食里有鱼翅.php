													<h2>北京饭店披露秦城监狱伙食里有鱼翅</h2>
						
						
																											<div id="postmessage_801690" class="t_msgfont">【中央社u台北28日电】 2010.08.28 03:03 pm <br />
 <br />
&nbsp;&nbsp;<br />
大陆关押重量级人犯的秦城监狱，文革时期部分监舍伙食比照部长级待遇，早餐牛奶、午晚餐2菜1汤、餐后苹果一样不少。专门从北京饭店调来的厨师，还会提供被误认为「粉丝」的鱼翅给人犯。<br />
<br />
「中国第一监狱」秦城监狱，位在北京市昌平区小汤山附近，是1950年代前苏联援助新中国经济与国防建设的工程之一。高级别、高规格、高待遇，让它成为特殊监管的别名。<br />
<br />
78岁的退休老人何殿奎，早在秦城监狱还称做功德林监狱时就担任管理员，退休时的职务是秦城监狱监管处处长。 「中国新闻周刊」2010年第12期，刊载了这位老人40年来的监管岁月。<br />
<br />
秦城监狱由4栋3层青砖小楼组成，编号分别是201、202、203、204监区，每座楼内部结构不一，其中204监区就是可以吃得到鱼翅、海参等高级食材的地方，何殿奎就是在这里第一次见识了鱼翅，当时还误以为那是粉丝。<br />
<br />
从何殿奎的描述得知，204监房铺着地毯，床是沙发床，伙食是专门到北京东华门「高干供应点」采购。其中饭后的苹果还是刚从冷冻库里拿出来的，「拉来时那苹果都冒着气儿。」<br />
<br />
这些人犯还可享受到1盒12块的固体饮料，1块能沏1杯柠檬茶；方糖分白色和咖啡色2种，「每天如此，即使在困难时期都一样。」<br />
<br />
何殿奎每天给他们送饭，每人1个4层的饭盒，分别装米饭、2道菜和1道汤，冬天用棉罩保温。每个饭盒颜色都不同以示区分，一共15份。<br />
<br />
这15人彼此不知道对方是谁，放风时严格隔离，绝对避免见面，狱警也只知道编号，唯一知道他们身份的，「除了领导，只有204监区管理员何殿奎一个人。」<br />
<br />
何殿奎追忆了几位人犯，例如当年情报界传奇人物潘汉年、前中共中央组织部部长饶漱石以及曾是毛泽东「5大秘书」之首的陈伯达。<br />
<br />
他回忆，陈伯达动不动就在牢房里表演撞墙，「搞得监狱上下越来越紧张。」后来又假装腿不能行动，最后还是靠何殿奎「制服」。<br />
<br />
毛泽东1976年9月去世的消息传开来后，怕中共高层忘了他的陈伯达重燃希望：「毛主席的后事办理完毕，江青同志会派人来和我谈话的，我的问题寄托在她身上。」但他等来的，是江青自己被关进秦城监狱。<br />
<br />
其实，「秦城监狱」4个字从未出现在中共的正式文件上，也没有挂牌，正式名称叫公安部预审局，现在叫监狱管理局。</div>

							
							
							
							
