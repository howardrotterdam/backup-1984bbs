但他们并不知道彼此。放风是严格隔开的，绝对避免见面。哨兵也只知道其编号。<br />
唯一知道他们身份的，除了领导，只有204监区的管理员何殿奎一个人。<br />
　　<br />
贴身监护潘汉年<br />
何殿奎跟这份专门看管大人物的特殊工作结下不解之缘，是因为潘汉年。<br />
1955年4月3日，他被从功德林的丁字号监区紧急抽调去监管潘汉年。领导看中了他的政治可靠和年轻肯干：他才23岁，但十来岁到延安，20岁入党，已经是个年轻的“老革命”了。<br />
那一天，何殿奎记得太清楚了，因为潘汉年太特殊了。潘是被秘密逮捕的，看守他的部队都不是那里的，是专门从北京市摩托连调来的一个排。整个监区，只关他一个人。岗是双岗，监房门口一道，监区门口一道，除了何殿奎谁都不能进去。<br />
潘汉年刚来的那十几天，何殿奎就住在他房间里，24小时贴身监护。<br />
那些天，潘汉年几乎每天被带去“谈话”。他出入都要戒严，如临大敌。<br />
谈话的地方不是普通的审讯室，而是会议室，坐在沙发上谈。主持谈话的先是公安部的一个局长。何殿奎在旁边端茶倒水。<br />
秦城的办案人员和监管人员有严格区分。前者即预审员，只处理案情；后者则负责犯人的吃、住、健康。没有特殊情况，前者不允许进入牢房，后者不可以知道案情。潘汉年的案子是唯一一次，何殿奎目击了审讯开始头几天的过程。<br />
谈话很“平稳”，但并不顺利。潘汉年就是不开口。僵持了几天后，公安部常务副部长徐子荣亲自来谈，说“保证你死不了，不信可以给你写个条子”，他才开始谈。<br />
潘汉年被捕时是来北京参加全国党代表会议的，这次会议是要解决被称为建国后第一次党内斗争的“高饶事件”。作为饶漱石的下属，潘汉年自知难逃干系，主动写了交代材料，包括自己曾被挟持去见汪精卫的那件事，结果为“饶、潘、扬反革命集团”的成立提供了“铁证”。<br />
不久，“饶、潘、扬反革命集团”的其他主犯相继入狱。<br />
何殿奎记得，潘汉年的编号是64，袁殊65，扬帆66，饶漱石最特殊，0105，全监狱独一无二。他至今不解其奥妙。<br />
谈起对潘汉年这位情报界传奇人物的印象，何殿奎说他个子不高，胖胖的，不爱说话，“很稳重的一个人”，从来看不出他情绪上的变化。他生活很有规律，晚饭后会在床上闭目打坐一个小时。刚看到这奇景，哨兵以为他死掉了，还曾跑去报告何殿奎。潘汉年在狱中写过一篇小说，写一个拉洋车的人，可惜后来找不到了。<br />
1963到1964年，204监区的犯人陆续释放。潘汉年就安置在秦城监狱的家属区，生活仍然由何殿奎照顾。那段时间，他生活比较愉快，常去南边的水库钓鱼，去附近的小汤山镇散步，和何殿奎也有说有笑。但这样的日子随着文革的到来彻底结束了。<br />
　　<br />
“我早就知道秦城有个何殿奎”<br />
文革开始后不久，扬帆、饶漱石、潘汉年等再次入狱。<br />
潘汉年等回到的秦城，已经不是文革前的那个秦城。“旧公检法”的那套管理制度，被造反派称为“修正主义”“和犯人同流合污”，全部取消。1967年11月，秦城被军管。原来的监管干部全部下放到“五七干校”劳动。<br />
何殿奎就此离开了秦城，一别5年。直到1972年11月，才被调回来。他是第一批回来的两个人之一。<br />
[!--CNW_PAGE--]<br />
秦城已经面目迥异了。因为“走资派”太多住不下，秦城又加盖了两栋4层红砖小楼，编号为205和206。监狱的名称也换成了“北京市军事管制委员会第七大队”。<br />
何殿奎告诉记者，“秦城监狱”文革前并不叫这个名字，叫公安部预审局，对外叫中华人民共和国公安部看守所。文革军管时写报告，落款是“七大队”，总理看了说：“什么七大队，不是秦城监狱吗？”从此秦城监狱才叫开了。但这个名称从来没有见于正式文件，也没有挂牌，正式名称仍然叫公安部预审局（现在叫监狱管理局）。<br />
何殿奎从“五七干校”回来后被分配到201监区。当时201关押了89名部局级干部。他是“负责人”之一，但是负不了责，上面有3个军管干部，他只能列席会议。<br />
不久，秦城监狱出了一件大事。铁道部原副部长刘建章的夫人刘淑清上书毛泽东，揭露秦城监狱里生活待遇低劣的问题。1972年12月，毛泽东在信上批示：“请总理办。这种法西斯式的审查方式，是谁人规定的？应一律废除。”工作组进驻，整顿秦城监狱，并逐个找在押人员谈话，问题逐渐得到解决。<br />
何殿奎回来一看，老熟人不少。公安部8位部长，5个关在201。放风时看见他们，只能点点头。<br />
潘汉年等也关在201。潘汉年苍老了许多，头发几乎掉光了。那几年，吃的是窝头白菜，见不到肉。<br />
当时秦城监狱最引人注目的是三位倒台的文革新贵，原中央文革小组成员：6821，戚本禹；6822，王力；6823，关锋。即，1968年进来的第21、22、23号。这种编号法从文革开始，一直延续至今。<br />
何殿奎说这三个人，王力很老练，一般比较讲道理；关锋精神失常了，经常骂街；戚本禹不大讲理，“造反起家的嘛”。戚本禹是个“闹监”的，经常在晚上大嚷大叫，吵得四邻不宁，还用手纸堵门上的玻璃观察孔，跟哨兵斗智。他还放言：“我戚本禹早就知道秦城有个何殿奎，看你对我采取什么措施。”<br />
这时是7月份。何殿奎把戚本禹迁到没人住的三楼，随他吵闹，随后打开外面的木门，只关里面的铁栅门，让他喂蚊子。戚本禹喊报告要找何殿奎，何让哨兵说“出差了”。三四天后再去，吃够了苦头的戚本禹终于愿意“谈谈”，从此基本上服管了。<br />
　　<br />
秦城监狱最难管的人物<br />
但戚本禹还不是最难管的。1975年4月26日，何殿奎被调到204监区，升任科长，专门负责管理“心眼太鬼”的头号难缠人物，曾任中央文革小组组长的陈伯达。<br />
陈伯达在整个秦城监狱都要算特殊人物。他曾是毛泽东的“五大秘书”之首，加上年龄又大，上面强调要绝对保证其安全，在生活上可满足其需求，以利于保存“活证据”。他被安排一个人住在204监区的二楼，两个哨兵日夜看着他。他动不动就在监房里表演撞墙，搞得监狱上下越来越紧张。<br />
何殿奎就是专门被调来啃这块硬骨头的。他首先搬到陈伯达旁边的监房中，近距离观察了一周，认定他的寻死只是作势而已。一天，陈伯达又要撞墙，被哨兵抱住。何殿奎赶到后，叫哨兵放开他：“我今天倒要看看你陈伯达撞个头破血流！”陈伯达不吭声了。<br />
1976年，陈伯达又开始折腾起来。他称自己腿不能走了，天天爬着上厕所。何殿奎估计他是装的，就让王医生去跟他说，要再不起来锻炼，他的腿就永远站不起来了。陈伯达害怕了，问能不能拄个拐棍。何殿奎说可以。“不用请示，当场我就答应他了。”<br />
何殿奎知道，陈伯达无非是想让上面注意他这个被遗忘的角落。1976年9月，毛泽东去世的消息见报后，他重燃希望，说：“毛主席的后事办理完毕，江青同志会派人来和我谈话的，我的问题寄托在她身上。”<br />
但他等来的，是江青自己被投入秦城监狱。<br />
何殿奎在204监区一直工作到1985年，提为副处长，脱离了监管第一线。他离开的时候，陈伯达等已保外就医，“王关戚”也先后被释放。潘汉年病逝在湖南劳改林场，没有等到平反的那一天。<br />
