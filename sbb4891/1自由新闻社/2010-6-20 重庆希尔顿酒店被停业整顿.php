<br />
　　重庆希尔顿酒店系一家涉外星级酒店。在该市打黑除恶专项斗争中，警方查悉，该酒店股东及相关管理人员长期与涉黑人员联系密切，对此立案、传讯。 <br />
<br />
　　重庆警方称，该酒店认为自己有背景、有关系，我行我素，肆无忌惮。2009年11月以来，因违法经营，重庆希尔顿酒店两次被公安机关查处。期间，中共中央综治办和上级公安机关来渝暗访时，亦发现该酒店违法经营行为，警方遂再次向该酒店提出警告。但该酒店不仅不整改，还对打黑除恶和公安机关依法管理颇有微词，甚至恶言诽谤，特别是利用涉外酒店可以制造社会影响的便利条件，在政务及外事接待中，故意制造麻烦，歪曲国家执法相关规定，扩大不良影响，严重损害重庆形象。 <br />
<br />
　　今年6月19日晚，重庆警方在对希尔顿酒店清查中，查获其经营管理者、酒店保安、行李员、前台登记员、酒店娱乐场所从业人员形成完整利益链条，与社会闲杂人员组成庞大的介绍、容留妇女卖淫团伙，实行“一条龙”服务，从中牟取暴利。同时，警方还在现场抓获犯罪嫌疑人102名。 <br />
<br />
　　经初步审查，重庆警方已刑事拘留22人。公安机关经审查发现，该团伙还涉嫌毒品犯罪。当晚，重庆警方对该酒店涉黑股东再次予以传讯，继续对其违法犯罪行为展开调查。 <br />
<br />
　　根据清查情况，警方20日决定，鉴于该酒店股东涉黑，违规经营，藏污纳垢，顶风作案；违反社会管理，并故意制造事端，影响重庆形象，依法责令其停业整顿。同时从已掌握情况看，警方发现有少数政法干警参与其中，为该犯罪团伙充当“保护伞”，违规执法。重庆警方表示，案件无论涉及到谁，都将坚决查处，绝不姑息。</div>

							
							
							
							
