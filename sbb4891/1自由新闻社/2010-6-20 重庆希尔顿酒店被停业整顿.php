																											<div id="postmessage_703875" class="t_msgfont">【世界日报u上海25日电】 2010.06.25 04:11 pm <br />
 <br />
&nbsp;&nbsp;<br />
因股东「涉黑」案牵出的重庆希尔顿「涉黄」案再曝内幕。据了解，该酒店下属的「钻石王朝」俱乐部，从高尔夫俱乐部到高档夜总会的「高级小姐」陪侍，其公关活动已成为一条龙服务，而在其向有关人士提供的「一条龙」服务中，不乏知名女艺人。此外，在涉及该案的人员中，除了前重庆市公安局副局长彭长健、原重庆规画局党组成员查红等官员外，香港女星刘嘉玲也被卷入其中。<br />
<br />
综合新华网及中广网报导，知情人爆料，重庆希尔顿股东彭治民与香港女星刘嘉玲关系十分不错，曾邀请刘嘉玲为其楼盘广告代言，费用超过百万。<br />
<br />
据报导，重庆希尔顿股东彭治民在外界眼中，神秘而少言，被当地媒体形容为「重庆最低调的企业掌门」。事实上，彭治民对外公开的身分是地产商，旗下重庆庆隆是当地知名地产公司，曾打造中国第一住宅高楼――海客瀛洲。 2002年3月，由庆隆投资的重庆希尔顿酒店成立，耗资8亿。<br />
<br />
之后，以希尔顿酒店为背景的钻石王朝俱乐部、庆隆南山高尔夫俱乐部分别在2004年与2008年建成。此外，彭治民旗下还有联星投资、金隅大成山水置业、庆隆希尔顿物业管理等多家公司。<br />
<br />
报导说，知情人称，「海客瀛洲」正是刘嘉玲所代言的项目。 2005年，由刘嘉玲、胡军主演的《好奇害死猫》曾在海客瀛洲取景，并应邀为该楼盘担任代言人。有媒体披露，除了获得不菲的酬劳，刘嘉玲还获赠一套价值百万以上的江景豪宅。<br />
<br />
另据爆料者称，彭治民在娱乐圈有着丰富的人脉资源，海客瀛洲因此成为影视大片的拍摄基地，央视大片《活着，真好》等均在此拍摄。报导指出，据称，彭治民还经常邀约地方官员和企业伙伴，前往庆隆南山高尔夫俱乐部打球，并洽谈生意。<br />
<br />
据报导，警方披露，在去年「打黑」行动中，彭治民曾多次被查，但还没有达到追究刑责的程度。在重庆希尔顿案后的第三天，彭治民被依法刑拘。据重庆地产界人士测算，彭的身家超过10亿人民币，成为当地落网涉黑富豪中身家最高者。</div>

							
							
							
							
