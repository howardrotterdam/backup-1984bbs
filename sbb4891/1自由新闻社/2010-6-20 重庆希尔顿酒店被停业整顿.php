<br />
　　从致歉信可看出,重庆希尔顿酒店将在停业整顿后继续营业。作为中国第一家因涉黑而被迫关闭的五星级国际品牌酒店,重庆希尔顿酒店此间备受各方关注,重庆市长黄奇帆日前在接受外媒访问时表示,重庆当局只是在处置犯罪的股东,无意针对诸如希尔顿的国际知名酒店管理集团。<br />
