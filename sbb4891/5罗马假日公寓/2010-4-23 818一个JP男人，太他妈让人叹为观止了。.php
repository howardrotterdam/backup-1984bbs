																											<div id="postmessage_579670" class="t_msgfont">先交代下背景，楼主是去另外一个城市的青年旅馆做义工。旨在磨练下自己的独立生活能力，楼主十指不沾阳春水，生活能力极差。马上要赴美留学，父母表示很担心，于是我想出去磨练一下。才有了这么一出。这个JP就是店长。<br />
之前递简历过去，店长跟我打了电话，大意就是我被录取了。然后交代了几点要注意的事项，其中有一条让我比较困惑：不许勾搭男老外。我当时就想，为什么一定要强调老外呢？你说一句不要和异性顾客走的太近就可以了嘛，干嘛一定要说老外呢？难道我够大中国人就可以了？当时我随便附和了几声。听声音，觉得店长应该是个温文尔雅的人，虽然他那句话让我觉得不适合。<br />
之后我就去了那家旅馆。第一次见到店长就想，不会吧，这个猪头哥一样的踩着拖鞋的男人就是店长？我想，青旅的气氛比较随意，人家爱穿拖鞋其实也不是什么大事，就没什么。店长说，你今天先自便，晚上我再跟你好好谈谈。我觉得也没什么问题。后来下午聊天的时候，他说你要去美国读书啊。我说是啊，他说我以前在法国学古典音乐的。我惊叹，那你法语不错咯？他说我混华人区的，表情相当得意。我就不说什么了。<br />
当天有一个客人是广州人，从广州骑车到这里，用了一个多礼拜。我下午没什么事，就和人家约着一起出去逛了下。<br />
下午都还很平静。<br />
到了晚上，我早上起早床赶得飞机，晚上实在是累了。店长说那我们谈谈。于是就把我带到了咖啡厅。他首先是在吧台的电脑里放了首自以为没有人知道的《蓝色多瑙河圆舞曲》，然后一脸无奈的跟我说，可能你不能接受这个，但是我每天必须要听下古典高雅音乐。我当时就傻了，虽然我社会阅历少，但是装逼是怎么回事，我还是知道的。之后，他从吧台里拿出一瓶红酒，说是法国原装进口的。我不懂，他非要我喝，我没办法，敷衍了两下。<br />
他一边喝酒一边抽烟，先说自己十一岁就独立了，以前玩重型机车什么的。像那种骑车旅行都是他以前玩了不要的。我点点头，没搭话。他接着说，我现在啊，就是提前进入老年阶段，每天晚上喝点红酒――以前都喝白酒――然后喝点功夫茶。然后他突然问我，你猜我多少岁。我想，不能太伤人，往小的猜，我说30？他脸就绿了。我说28？他说其实他23。我当时就orz了，你TM23玩个P的颓废啊。<br />
后来我实在撑不住了，不停的打呵欠，他看见了，说有这么困啊，来来来，我们继续聊。当时楼主就想你他妈有病吧！我最厌恶的就是人家打扰我睡觉什么的。<br />
后来我随便扯了个理由，睡了。<br />
这是第一天。</div>

							
							
							
							
