													<h2>我在译言的第一篇译文，求鼓励</h2>
						
						
																											<div id="postmessage_848251" class="t_msgfont">在译文注册了个ID，今天译了第一篇不算长的文章，但因为英文不太好花了一整个下午。<br />
按“信，达，雅”的标准向各位老师求教，请组员指点一二。谢谢。<br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; Survival tips: what to do if an animal attacks you<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; 生存贴士:如果动物攻击你，怎么办呢？<br />
<br />
&nbsp; &nbsp; If it's a monkey, talk football, if it's an elephant play dead … and other helpful ploys<br />
<br />
&nbsp; &nbsp; 要那是一只猴子，可以谈论足球，要是一只大象那就装死…还有其它有用的伎俩 <br />
<br />
<br />
<br />
<br />
<img src="http://static.guim.co.uk/sys-images/Guardian/About/General/2010/9/24/1285349367794/Black-bear-in-a-field-006.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
&nbsp; &nbsp; When a woman in Montana was attacked by a black bear, she threw a courgette at it. Photograph: Alamy<br />
当一个蒙大拿州女人被一只黑熊攻击，她朝它扔小胡瓜。摄影：阿拉米<br />
<br />
&nbsp; &nbsp; Thanks to a plucky woman from Frenchtown, Montana, we now know how to deal with at least one animal menace. Attacked at home last week by a 90kg black bear, she sent it lumbering back into the night simply by throwing a courgette at it. Local police described this as &quot;improvising&quot;. The vegetable-projectile approach will probably fend off all sorts of creatures. But if you don't fancy putting it to the test, here are some alternatives.<br />
<br />
感谢勇敢的来自蒙大拿州弗伦奇敦的一个女人，我们知道了怎样处理（来自）至少一个动物的威胁。当上周她在家中被一只90公斤的黑熊攻击时，她迅速向黑暗中缓慢移动的黑熊投去一只小胡瓜。当地警察把这形容为“即兴法”。这种用水果抛投的方法或许能挡开很多种动物。但如果你不想这样做，这里有一些备选方案。<br />
<br />
&nbsp; &nbsp; Hngry monkeys: You could simply give the macaque your Magnum. Failing that, try the &quot;open-mouth threat&quot; C make an O with your mouth, lean forward and raise your eyebrows. Then back away slowly. If that doesn't work, open a can of beer and talk about football (they're a bit more afraid of men than of women).<br />
<br />
饥饿的猴子：你只需简单的给猴子一只大酒瓶。如果不行，尝试“张嘴恐吓法”-做出一个O的嘴型，（身体）向前倾斜并扬起你的眉毛。然后慢慢地撤退。如果还不奏效，打开一罐啤酒大侃足球（比起女人猴子们害怕男人多一点）<br />
<br />
&nbsp; &nbsp; Killer bees: Africanised honeybees aim for your mouth and nose first. So pull your top up over your head, then run, run, run away. Don't give up too soon C they've been known to chase victims for more than 400 metres. If possible, shut yourself in a car or building. Diving into water won't help C they'll wait till you come up for air.<br />
<br />
致命的蜜蜂:蜜蜂首先会以你的嘴巴和鼻子为目标。那么拉起你的上衣盖过头顶，然后跑，跑，跑开。不要太快停下来 C 很多人都知道它们会追击受害者超过400米。如果可能，可以把自己关在一辆车或建筑物内。潜入水中不起作用：它们会等你上来透气。<br />
<br />
&nbsp; &nbsp; Crocodiles and alligators: Whatever some idiots tell you, crocs and gators cannot run faster than racehorses. On land, even humans have a good chance of outpacing them. Forget anything you've heard about zigzagging C just leg it. If the reptile gets you into its mouth, don't waste time trying to pry its jaws open. Stick your thumb or finger into its eye. The pain and shock should make it release you.<br />
<br />
鳄鱼和短吻鳄：任凭一些白痴怎样说，鳄鱼和短吻鳄终究是跑不过赛马的。在陆地上，连人也有很大机会超越它们。忘掉你听过的之字型（路径）之类的 C 只管跑开吧。<br />
<br />
&nbsp; &nbsp; Sharks: If it's trying to take chunks out of your boat, hit it with a paddle or a pole. The vulnerable spots are the eyes, gills and snout. If you're in the water, you may be able to escape by rapid changes of direction. Sharks are not very manoeuvrable. If you're in its mouth, do not play dead. Eyes, gills, snout, remember.<br />
<br />
鲨鱼：如果它试图从你的船里抓出矮胖的人，用船桨或撑篙打它。它脆弱的地方是眼睛，腮和口鼻部。如果在水里，你也许能依靠方向的快速变化来逃亡。<br />
<br />
&nbsp; &nbsp; Elephants: Plan A: Climb a tree, first making sure it's big enough that it can't be pushed over. Plan B: Play dead in the hope that Jumbo will get tired of tossing your body around.<br />
<br />
大象：方案A:爬到树上去，但得保证它足够大不会被推倒。方案B：装死，希望那只庞然大物厌烦在你身体周围辗转。<br />
