													<h2>刚工作怎么投保最省钱</h2>
						
						
																											<div id="postmessage_791201" class="t_msgfont">By 张翼轸<br />
六月，对于应届大学生而言，是吃散伙饭，准备踏上职场生活的日子。工作，不仅意味有了收入，也意味着更多的责任。本着对自己对父母负责的态度，拥有一份保险无疑是一种可取的态度。不多，刚刚踏上工作岗位，绝大多数人的收入都有限，尤其是“新上海人”，房租餐费七七八八扣掉后也就剩不下多少了，而许多保险高昂的年缴保费往往让人望而却步，这也是许多人没买保险的关键原因。其实，对社会新鲜人而言，以合理的投保策略购买合适的保险很重要，这样才能以尽量少的保费支出过得尽可能多的保障。<br />
<br />
投保省钱四大思路<br />
<br />
对于可供支配收入有限的社会新鲜人而言，怎么投保才能省钱呢？<br />
<br />
减少投保的险种抑或降低保险的保额，这是许多人正在采用但却是极不可取的方式。如果投保人的确囊中羞涩而且购买的保险产品已经是最优化最便宜的话，上述的做法的确无可厚非。但事实上，许多社会新鲜人往往因为不正确的保险理念或者代理人的误导，购买了不适合自己的保险从而造成保费开支过高――对于这样的情况，更改保险产品优化结构才是关键。<br />
<br />
对于社会新鲜人，本着节省保费的目的，选择保险产品当遵循以下思路：<br />
<br />
1.以重大风险防御为主。如今保险产品很多，针对的风险也是五花八门。由于经费有限，所以我们要把钱用在刀刃上，以重大风险为主。什么是重大风险，诸如意外身故、残疾或者罹患重大疾病，这些对于自身以及家庭无疑都是致命性的打击，要么意味着没有了没来的收入来源，要么意味着巨额的医疗开支，这类风险需要重点防范优先考虑。而至于传统的医疗报销或者医疗补偿，几百或者几千元的医疗费用相比就算不上致命性的打击，就可以放在上述保障完备且有余力的前提下才考虑了。<br />
<br />
2.不考虑返本类保险。许多人不愿意为保险花钱，因为他们觉得一旦没有出险保费就打水漂了。所以这类人购买保险，往往会选择返本型的产品，一旦没有出现所有保费全部返还，心理上要好受需多。但是，返本是有代价的，那就是保费的大幅增加。返本型保险，说到底不过是一份投资产品+一份保险的组合。假设某保险年保费是100元，市场收益率是4%，那么要将此款产品设计成返还型，保险公司就会问你收取2600元保费，用其中的100元直接购买原来的那款产品，然后将剩下的2500元为你赚回这100元。对于社会新鲜人而言，购买返本型保险，保费压力会相当大，迫使你降低保额，这无疑是不可取的。<br />
<br />
3.缩短保障期限。理财要有长期规划。正因此，许多人购买保险时，往往会选择最长的保障期限，譬如终身或者20年。但是，这同样意味着年保费会大大增加――由于寿险类产品年龄越大当年保费越高，为了避免晚年投保人缴费压力过大，保险公司往往会采用均衡保费，即最初几年保费会高于实际保费，而后期的保费则低于当年实际保费，早期多收的保费储备着用于后期高保费时垫付，当你选择的保障年限越长，那么就意味着你提前垫付的保费也越多，对囊中羞涩的社会新鲜人而言，无疑是一种浪费。所以，保险期限不妨尽可能的缩短，比如5年这样的时间段。5年过后，也许你的收入水平和经济状况已经有了大幅的改善，这时再考虑长期的规划也不迟。<br />
<br />
4.选择自然保费保险。虽然5年的保障期限相比10年或者20年而言，初期垫付的保费大大降低，但这终究还是有垫付因素存在的。所以更彻底的策略便是选择自然保费，当年实际保费该是多少就是多少，如此可以将保费开支降到最低。<br />
<br />
在以上四大宗旨下，下面就将搭建一个基本的投保组合，尝试以尽可能少的保费支出来获得尽可能多的保障。<br />
<br />
意外险：纯意外保障即可<br />
<br />
意外险，可谓是性价比最高的入门型保险。对于社会新鲜人而言，也是第一份保险的最佳选择。选对合适的意外险，可以以很低的保费实现颇高的保障。<br />
<br />
以国泰人寿的意外宝综合意外保障计划为例，50万元保额的年保费仅为250元，每天的保费支出不到7毛钱。国泰人寿在此款产品的基础上还有一款升级版全面型的产品，在50万元保额的基础上，再增加100万元的航空意外险和100元/天的意外医疗住院补贴，其年保费为340元，对于那些工作经常需要出差的社会新鲜人而言，后者无疑更为合适。<br />
<br />
国泰人寿之外，泰康人寿的e顺综合意外保障计划同样是一款值得考虑的意外险产品。同样50万元保额，其年保费为275元，较国泰人寿的意外宝略贵，但依然较市面上许多同类产品便宜许多。相比国泰人寿意外宝，泰康e顺综合意外保障计划的优势在于除了可以选配意外医疗住院补贴外，还可选配意外医疗保障，以实现对意外医疗费用部分的报销。当然，意外医疗属于保费相对较高的附加险，仅1万元的保障年保费便达39元――若从预防重大风险的角度，还不如花费41.4元购买90万元e顺交通工具意外保障计划中的航空意外险保障。<br />
<br />
上述两款产品，均可以通过保险公司的官方网站进行投保并且在线支付，对于伴随网络长大的新一代而言，无疑是最方便不过了。<br />
<br />
重疾险：自然保费更省钱<br />
<br />
还有什么比一个刚刚踏上工作岗位花样年华的年轻人罹患重疾更令人扼腕的事情。这不仅意味着当事人可能因此失去工作失去经济来源，而且治疗重疾的费用对普通家庭更是高昂的压力。正因此，重疾险亦是社会新鲜人不可或缺，应当放在意外险之后考虑的险种。<br />
<br />
购买重疾险，有两种选择：均衡保费的传统险种和自然保费的附加险种。<br />
<br />
先来看均衡保费的传统险种。以费率相对较低的昆仑祥宁定期重大疾病保险为例，若是1987年8月1日出生的女性，投保20年保障20万元20年缴费的话，那么年缴费为500元；若是男性，则年缴费为600元。此款重大疾病险同时还具有20万元意外身故的保障。对于那些怕麻烦同时手头相对宽裕的社会新鲜人而言，这样的产品无疑购买了比较省心，每年只需要银行代为扣费即可，这也是最主流的购买重疾险方式。<br />
<br />
当然，若我们希望进一步降低保费，或者增加保额，那么就需要考虑选择自然保费的重疾险了。由于自然保费的重疾险不以主险的方式存在，所以我们必须首先购买一款主险，然后以附加险的方式来购买自然保费的重疾险。<br />
<br />
以泰康人寿的储值型大病保障计划为例，其核心是e理财万能险，然后可以通过附加险的形势附加重疾险。就费率而言，泰康e理财万能险在同类产品中是较低的，除投保时收取2%的初始费用外，主要费用就是从第二年开始每月5元的保单管理费用；而且泰康e理财万能险的收益也不错，结算收益始终在同类产品中居前，最新利率为4.15%。而且此款产品最低缴费为2000元，对社会新鲜人而言压力也比较小。在投保了泰康e理财万能险，享受相对稳健的收益之后，便可以投保其中附加的重疾暨身故附加险了，相比传统重疾险，这款附加险还包括了传统寿险的身故保障。以22岁的男性为例，20万元保障的年保费为356.4元；而女性为317.04元，显然均较均衡保费的险种来得低，2000元保费大体可以支撑五年的缴费需要。当然，在缴费金额不变的前提下，投保人完全也可以选择提高保额的方式。以22岁的女性为例，30万元保障的年保费为475.56元；男性则为534.6元，在略微省钱的前提下，最初的保障可以提高50%。<br />
<br />
寿险：定期寿险优于终身寿险<br />
在有了意外险和重疾险的保障之后，接下来需要考虑的就是寿险保障了。寿险保障无疑是对意外险和重疾险的一个补充，当你不是因为意外而身故或者罹患重疾险保障之外的疾病而身故时，寿险就能发挥其不可或缺的作用了。<br />
<br />
在购买寿险上，定期寿险无疑是比终身寿险更佳的选择，后者因为保障期限过长，所以其保费相较保额的比例过高，以某款终身寿险为例，2万元保费才可以获得5万元的终身寿险保障，这对于社会新鲜人而言显然是一个糟糕的选择。<br />
<br />
所以，在购买寿险时，不带返还的定期寿险是一个不错的选择。以新华人寿的定期寿险为例，50万元的保额10年的保障期，22岁的男性年保费为550元，而女性则为300元。显然保费压力并不算大。<br />
<br />
当然，秉持上文提及的自然保费的思路，我们同样可以采用附加险的形式投保。泰康e理财万能险除了重疾险外，还可附加寿险。同样50万元的保障，22岁的男性年保费为345.96元，而女性为157.56元。当然，这里需要指出的是，无论是重疾险还是寿险，自然保费的品种其保费都会伴随年龄上升而快速上升，同样50万元的保障，到了30岁男女的保费就会上升至440.52元和203.04元，而到了40岁就会攀升至857.52元和414元。</div>

							
							
							
							
