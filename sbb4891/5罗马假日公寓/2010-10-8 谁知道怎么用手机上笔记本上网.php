采用带有GPRS功能的手机与笔记本相连来上网，通俗一点说，是将带GPRS手机作为一个外置modem来建立相应GPRS拨号连接。但要说明白的是，不是所有市面上宣称带有GPRS功能的手机均可以与笔记本相连的，这主要是因为部分手机生产商没有提供相应连接线或驱动程序，这一点国产品牌手机比较明显，徒有其名没有实际效果。 <br />
<br />
综合GPRS手机与笔记本连接上网的接口方式，也就是按接口类型来分又可以分为数据线、红外、蓝牙三种方式。 <br />
<br />
一.数据线方式： <br />
<br />
数据线方式是一种比较传统的方式，又可以细分为COM连接线、USB连接线两种，只要笔记本有COM端口或USB端口并有相应连接线即可，手机生产商基本上均提供了GPRS Modem for Win9X/2000/XP的驱动程序，可以参照相应手机生产商的官方网站。采用该方式就要求笔记本具有COM口或USB口与数据连接线、手机生产商提供相应驱动程序。部分较新笔记本不再提供COM端口，则需要购买USB转COM口转换线或pcMCIA转COM适配卡。 <br />
<br />
二.红外方式 <br />
<br />
红外线方式是目前GPRS手机与笔记本相连方式中较为常用的方式，大多数笔记本均提供了红外端口，速率基本上为4M bps，用于GPRS上网是足够了。并且在软件上不太需要手机生产商提供专用驱动程序，特别是Windows XP环境下市面上大部分手机均可以正常识别出来，较容易建立拨号连接。但由于手机与笔记本红外端口必须相对在一个较小角度范围内，有效传输距离较短，由于手机红外发射功率问题，与手机连接时应该将距离放到30厘米以内，有效传输角度0～30 度。不适合于乘坐汽车或移动中的用户。可以想见采用这种方式上网，首先要求手机有红外接口，其次笔记本也需要内置红外接口，或者购买COM口转红外转换器、USB口转红外转换器，约100-200元。 <br />
<br />
三.蓝牙方式 <br />
<br />
蓝牙端口方式是一种比较前卫的连接方式，在该方式下，手机可以距离笔记本一段距离，不象数据线方式下因线长而限制手机的摆放，也不象红外接口方式下对手机、笔记本摆放位置的严格限制，在蓝牙接口下，GPRS手机可以在10米范围内任意摆放，适合于移动中的用户。但采用这种方式的成本最高，条件最苛刻，一方面要有蓝牙GPRS手机，另一方面要笔记本带有蓝牙适配器。但笔记本带有蓝牙适配器的不多，采用比较多的变通方式是利用蓝牙适配器（USB形式或CF形式），约为400-800元左右。 <br />
<br />
具体的设置是： <br />
<br />
1、手机设置 <br />
<br />
当前中国移动的Internet的APN是预设的，一般不需要调整。但有时可能人为原因改动了参数，这里以摩托罗拉E398手机设置为例说明一下，“网页设定”-&gt;“新条目”，如检查到各项配置均为空，选择编辑“服务类型1”，一定要设为“CMNET”，登陆名称与密码均为空。有的时候论在笔记本如何调整均不能上网，请先检查这项设置。 <br />
<br />
2、笔记本设置 <br />
<br />
接口驱动 <br />
<br />
COM口或USB口，无论是Windows 9X/2000/XP系统均可以正常驱动，只要CMOS设置启用该设备，均可以正常识别，然后将数据线连接到相应的端口上。 <br />
<br />
笔记本红外接口配置 <br />
<br />
在Win9X下看一下你的系统的右下角（即系统托盘），有没有一个红外的图标？要是没有的话，你需要在“添加新硬件”里添加一个标准的红外线设备，98SE里显示为“常规红外线串行端口或硬件锁”，然后按默认的配置，把它虚拟为com4和lpt3端口。如果有，好办，双击点开，点“选项”，把“启动红外线通讯”打上钩，其它的一律不必设置。 <br />
<br />
在Win2000下，需要安装微软Windows 2000红外线驱动补丁 Q252795_W2k_sp3_x86_cn.exe，如已安装Windows 2000 SP3补丁就不需要安装该补丁。在XP下无须进行另外的设置。 <br />
<br />
蓝牙端口 <br />
<br />
无论是笔记本内置还是蓝牙适配器，只要安装相应驱动程序后就可以正常驱动该设备，请参照笔记本或蓝牙适配器安装说明。 <br />
<br />
3、手机驱动 <br />
<br />
数据线方式 <br />
<br />
以索尼爱立信K500C为例； <br />
<br />
首先下载一个K500的拨号软件。进入手机“连接”里“数据通信”的“数据帐户”。随意添加一个帐户名进行如下设置； <br />
<br />
APN：cmnet <br />
<br />
用户名：空 <br />
<br />
密码：空 <br />
<br />
然后点保存，回到“数据帐户”里，选择你刚建立的帐户名，点“更多”。在帐户信息里查看外部ID，如果是1，拨号时就用*99***1#；2就用*99***2#。 <br />
<br />
红外接口 <br />
<br />
在Win9X下安装手机厂端所提供的红外驱动程序，记住是针对红外端口，不要把针对数据线的驱动程序用在此处。 <br />
<br />
在WIN2000/XP正面，打开手机的红外，笔记本上面只会发现红外设备，并不会给手机安装驱动程序，蛤WIN2000是给红外手机分配一个标准的红外MODEM，所以很多的手机是不支持这样情况(而西门子3618,6618则支持)，在这种情况下你必须安装厂商提供的驱动程序才可以继续后面的设置。而在XP下面是预置了很多手机的驱动程序的，打开手机的红外时XP会自动为手机安装好驱动程序的，如果手机型号特别新而XP不能安装驱动程序你同样也要找到厂商的驱动程序才可以继续。 <br />
<br />
蓝牙接口 <br />
<br />
此处以飞利浦820为例。 <br />
<br />
从手机主菜单，选择“手机设定”→“蓝牙设置”→“蓝牙设备”→“检测”；运行笔记本内的“蓝牙管理器”，刷新，找到820手机，右击“蓝牙管理器中”的手机图标，选择信任设备，此时手机出现配对提示，输入任意的号码，在“蓝牙管理器”中，输入相同的号码，此时手机会找到电脑，完成信任。可以看到，蓝牙接口方式的设置是最麻烦了！ <br />
<br />
4、建立拨号连接 <br />
<br />
通过接口驱动、手机驱动、我们已将手机作为一个Modem设备来识别，按照普通建立拨号连接的方法建立拨号连接。蓝牙接口方式需运行专用的“GPRS蓝牙拨号连接”建立连接。注意如果接入号设为*99#不成功的话，就改试*99***1#或*99***2#，一般情况均可以成功上网。我们可以互联网上遨游驰骋了！</div>

							
							
							
							
