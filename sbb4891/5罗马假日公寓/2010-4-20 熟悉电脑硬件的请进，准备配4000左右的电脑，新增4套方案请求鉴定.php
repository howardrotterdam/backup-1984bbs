呵呵~昨天下班下的急,要赶车回家吃饭,今天给你补充一下<br />
1.电源不会浪费的,如果买过头了,等电脑升级或者换平台的时候就可以不换电源了.不过你只有一个牌子我也不知道你的什么电源.<br />
选电源还是要选一些比较主流口碑比较好的牌子,看额定功率,峰值功率,这个非常重要,要自己算,主板显卡各大件的满载功率,留出一定安全系数.一般不上独显,搞个300W的,独显的都主张上400W.电源不掉价,可以买好一点的,不浪费.<br />
以前看过一些专门玩硬件的大牛写的测评文章,内容也不大记得了,反正电源猫腻也很多,那人测了主流的十几款电源,只有一款合格.不过这些都是极端案例,不玩超频,不玩交火SLI之类,其实也都没问题的.<br />
2.我刚才看了一下回复,你好像问到映泰,我家用的就是映泰的板子.我是一线主板都用过,中低端,因为我也都是配自己用的电脑,没钱烧高端板子,然后是映泰,硕菁,百利德(这个山寨牌子,估计没人听过),也用过垃圾电源,那个垃圾电源烧了我两块板子,现在的一台五年前的电脑跑的是百利德的板子.一线产品自然没啥说的,确实很稳定,映泰现在的做工非常好,我家用得也不错,硕菁那个板子二手的,没用多久,印象不是很深刻,感觉也还行,百利德是不能碰的,否则够你烦.那牌子太山寨了,性能不行.映泰用来超频玩很爽,主板自带超频工具.不过映泰的板子并不便宜.<br />
3.硬盘选你够用的就行了,潮流是赶不上的,而且以后硬盘只会更便宜,大不了以后加一块.<br />
4.32位系统理论最高支持4G内存,实际3G多点.2G+1G很别扭,1G*3组三通道的板子很贵,所以如果你用32位系统没有必要浪费钱,就算以后上64位系统,大不了加一条内存.<br />
5.除了显卡危机类游戏,孤岛,永恒之塔等,集显足够你用了,现在很多集显自带显存,WOW和星际2都能玩,不过可能开不了全高效.<br />
6.AMD没问题,CPU都够用的,选你喜欢的,用起来心里舒服<br />
7.网上买注意选口碑好的商家,网上龙蛇混杂.不去电脑城的话,那些电脑城的注意事项就不必说了.走网上还是不错的选择,不过可能要自己要装机,装机没问题就没什么要说的了.<br />
8.我一向主张买电脑够用就行,自己喜欢什么就选什么,不管多好的单子都有人弹有人夸,因为大家都有自己的偏好.4000左右的电脑也不低了,属于中端,是比较有技巧的一档电脑,配好了性能能上高端性能,配的不好可能不如低端电脑.如果你在性能方面要求不高,又有中端的预算,把钱花在外设上,譬如配个好一点的音箱,别以为看高清就只是视觉享受,没有声音配合,什么都白搭.最重要的是,外设保值,最不保值的就是机箱里头的东西,机箱都比那堆东西保值.外设可以一次投入长期使用,里头的东西觉得不够用了就升级一下,比一步到位买台高端电脑划算不止一点点.<br />
9.上面回帖里看到个CPU很高,其他都缩水的配置,不推荐这样配电脑.因为现在的性能瓶颈不在CPU,不要浪费钱.一般来说有显卡的,显卡:主板:CPU(钱的比例)~=~1.2:1:1,没有显卡的,主板:CPU~=~1.5:1.机箱要大机箱,哪怕你买小板,小机箱有散热问题,坏硬件.机箱可以挑个稍微好点的,好看点自己看着也舒服.机箱主要看做工,封口平整密封的就好,板金厚的也会贵一些,看你的预算决定.<br />
10.具体型号帮不了你啥忙了,因为好长时间不关注了,一些原则性的问题把握住了就行了.电脑配置的个人喜好差异实在太大了,跟着自己的喜好走就对了,只要电脑不是配出来用不了,就不要理会别人怎么说,重要的是自己用着的感受好不好.<br />
