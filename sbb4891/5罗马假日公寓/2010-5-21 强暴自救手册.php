　　 对付坏人决不能手软！这是很多人经历了痛苦的现实后得出的结论。尤其是单身的妇女和儿童，在遭遇坏人时可以“智取”而胜之。<br />
<br />
　 　“智取”是“寻找一种用欺骗的方法去战胜对方”。比如示弱后用膝撞、肘击、头撞等类似动作。而妇女和儿童的很多先天不足是犯罪分子猖狂的原因，比如他们力量较小，速度慢而且胆子小，不敢同对方纠缠。现在给大家介绍国外的对付坏人的自卫功夫，我们都可以用，这就是所谓的“齿道”。　<br />
<br />
　　顾名思义，“齿道”就是用自己的牙齿攻击别人，保护自己的人身和财产安全的手段，说白了，就一个字：咬！<br />
<br />
　 　很多人认为这是一种不光彩的功夫，不应该是光明磊落的英雄所为。但却在防卫中占据着重要的地位，使一些妇女儿童免遭坏人的迫害，而且效果非常明显。这是此处转载的原因。<br />
<br />
