<br />
　　如果你和对手身体交叉，就咬他的面颊、耳朵或者脖子！<br />
<br />
　　齿道的练习：<br />
<br />
　 　首先你要清楚一口咬进多少肉最为合适。通常人们认为越多越好，其实这是不科学的。咬进口中的肉量必须适中，让你可以反复不停地、左右拧头地咬，发挥出你最大的力量！<br />
<br />
