<br />
3. 安定门内大街157号谢家胡同----&quot;桂林魅&quot;桂林米粉店:招牌牛腩粉7元,素什锦粉5元,北京少见的9寸海碗,一共40多种米粉,地道的桂林风味,据说卤汤是由50多种香料做的,在胡同口就能闻到,扑鼻的香,地铁往南150米就是,巨棒!<br />
<br />
4.首都剧院斜对面（往南斜）有个兰州牛肉面馆，门脸不大，卫生也一般，但味道真是不赖，4元一碗，在王府井那里算极便宜了。<br />
<br />
5.地安门十字路口的清真小吃店，那的豌豆S（个人认为）北京最好吃。。。。。<br />
<br />
