34.紫龙宾馆（现在叫渝信川菜），那里的樟茶鸭是我吃过的最好吃的。具体位置应该是在宽街中医院对面那条胡同里的紫龙宾馆！<br />
<br />
35.在新街口附近（听说的，尚未有时间去证实）有家正点的回民馆子，正试营业期间，所有羊蝎子免费，容器比脸盆还大，据说好吃的连汤儿都一滴不剩，有兴趣的报名，让*威请咱们吃--我多仗义，尽找便宜又实惠的！<br />
<br />
36.新街口豁口，老桂风米粉店，老字号，听说好吃极了。。。。。<br />
<br />
37.我也知道一地儿，驴肉火烧巨好吃，在大红门儿一带，具体位置说不清楚，反正我找得着。<br />
<br />
38.外馆斜街，花诚酒家的水煮鱼棒极了，辣嘴不辣喉咙！香，18元儿一斤！<br />
隆重推出：黄花城下面最早的开张的仓坡扶饭庄，里面的虽然没有大厨。可是山村风味爽极，几乎都是美味，而且店里极为干净。现介绍几道我去那里必点的菜：凉菜：水芥菜（只长在清泉或者干净的水边，正宗野菜，有点芥末的味道，清口）驴蹄筋（是老板亲自做的，干净，味道极好）热菜：松蘑炒肉（配料：松树下的蘑菇和新鲜猪肉，想想滋味吧）摊柴鸡蛋（那叫一个香）熬小鱼（黄花城水库的小鱼）还有再点上一张家常饼，还等什么，暴撮吧！<br />
<br />
39.平安大街赵公口路口西南角有个贵州餐馆&quot;竹笋情&quot;，风味独特，进得门去先让你课瓜子，是那种最土制的瓜子，从葵花里抠出来就扔热锅里炒的那种，一开始我还懒得课，后来就停不了嘴；当然菜色也好，瓦片蒸腊肉、香肠（可以各来半份），竹笋菜，尤其笋干鸡汤，不赖。爱辣的同志不仿去尝尝，价格算合理吧。<br />
<br />
40.在崇文门哈德门饭店东侧(108车站再过去一点)有一个赣南饭店，只是一个比较小的饭馆，但里面的赣菜一绝，而且特别便宜，四、五个人吃饭点小炒鱼，酸菜肚尖，三杯鸡，炒米粉等菜(小份)加起来不超过60.00元。好吃极了。它门口有一个老北京，就沿街，非常好找。可以去试试。<br />
<br />
41.后海南岸的&quot;孔已己&quot;，江南风味，散装黄酒，用锡壶温热了喝，爽！小菜做的也特地道！<br />
<br />
42.还有一处是祁家豁子55路总站，下车就是，跟住家屋门差不多大小的一个门脸儿，很正宗的西安风味，尤其是油泼面。全家吃饱吃好不超过20元。不过得看各人口味了，要喜欢吃西安面食的那真是踏破铁鞋无觅处啊。<br />
<br />
43.翠微大厦北头，马路东边有家煎饼店，种类丰富，不同于街边煎饼车上的那种，常常排大队的！和平门三千里烤肉附近有家小馆，卖一种&quot;燃面&quot;，乃宜宾特产，据说极香，不过不知具体位置。<br />
<br />
44.朝阳门，向南，二环外，富华大厦，东侧，胡同（东西走向）向东20米，在胡同北侧，有一小门脸，红色，名字忘了，专做肥肠，各种味道，非常棒！！！！！<br />
<br />
45.甘家口路口，东西走向方向，向西50米左右，路北（钓鱼台北门 往西斜），名字从没记过，牛肉拉面、凉面（夏季供应）、各式小菜、小碗牛肉，都特棒！！！！！我从高一的时候就在那里吃，到现在已经10年了，净是回头客。<br />
<br />
46.你们说的三元，在西四附近也有一家，在西四肯德鸡对面，门脸很小。<br />
<br />
47.西四环内，定慧寺桥往北，第一个路口向东，400米左右，路北，名字又不记得了，那里的春饼巨棒！！！！<br />
<br />
48.甘家口进口向左转50米处有一叫双龙的烤肉店，吃完终生难忘。<br />
<br />
49.颐和园北宫门往西的一条路（通向青龙桥大桥），有一家&quot;马记烧饼铺&quot;，那里的烧饼夹肉3块一个，味道棒极了！！！！还有杂碎汤，如果不吃里面的杂碎只要汤是免费的。随便喝。<br />
<br />
50.我可是从小就在那里吃了，别的地方的烧饼夹肉我看都不看了呢~~现在早上有很多去香山颐和园遛弯的老头老太太都钟情于那里~~这家店上过《北京晚报》。有机会大家去TRY一下拉~~~~~~<br />
<br />
51.蓟门小区里有一个地方叫&quot;独当一面&quot;，里面有一种鸡腿饭，整只的鸡腿，油菜，西红柿还有一个煎蛋，爽急了！！！！<br />
<br />
52.SOGO斜对面&quot;妞妞饺子馆&quot;。我从不吃饺子的可到了那......<br />
<br />
53.新街口，爱图美再住北，路西，有个重庆（也可能是成都）小吃店，很好吃，每次往那边溜达总要去吃。。。。。<br />
<br />
54.鼓楼东北角有一家小的露珠火烧店,<br />
<br />
55.宣武门,过了SOGO向南一公里,路西,有个小的牛肉拉面店<br />
<br />
56.东四７条东口向胡同内走１００米，&quot;朱家菜&quot;味道不错，非常家常菜，好吃便宜！适合饿了的时候去！<br />
<br />
57.工人体育馆北门正对马路的北边有个&quot;味正园&quot;韩国烧烤，无烟碳烤味道绝好，便宜实惠，（两人吃好在５０元左右，可吃到２－３种烤肉！）<br />
<br />
58.方庄家乐福对面有个刚记广州海鲜，很正宗的广州菜，大连鲍28一只，扇贝才3元一只，基围虾38一斤，开始时我也觉得那儿可能挺贵的，结果两个人150元都快撑死了。<br />
<br />
59.大觉寺的糯米藕北京一绝。。。。。这是货真价贵的一绝哟！吃了他那的，别处的就觉得根本没法吃了。。。。。<br />
<br />
60.世人个个学长年，不知长年在目前，我得宛丘平易法，只将食粥致神仙宏状元，不论甜粥咸粥都好喝的没话说！好几家店呢。。。。。车道沟啦，展览路啦，花园东路，安华西里一区什么的地方都有。。。。。我去的是它的伸粥店，西三环北路93号。。。。。。。<br />
<br />
61.西苑饭店旁十子路口东北角出租之家，有10多种热菜（现炒的小炒，味道绝对地道，有点怀旧的感觉，80年代中期菜馆的手艺，棒！量大）另外：凉菜、主食、汤自助，才8元/人。<br />
<br />
62.隆重推出：北京第一家个人菜馆，绝对正宗北京家常菜。就在一条幽深的小胡同里面，提示：（东城，胡同名字-翠花）特色菜：五丝桶、蒜香肘子、沙锅丸子、扒白菜、红烧平鱼、京酱肉丝！这个可是我好不容易才找到的！好好享受吧！<br />
<br />
63.鼓楼地铁西南口出来有一家名为庆宾的涮肉味道一流，要早去晚了没位价格适中<br />
<br />
64.全北京最爱的水煮鱼是：西坝河的红京鱼水煮鱼，地点在重庆饭店旁边的臭沟边，是总店，店面不大，不远处的远方饭店的一层，是另一家分店，分店对面有一个写着&quot;台球&quot;字样的门洞，进去后是另一个分店，所有的店在六点前订坐有效，过期作废，而且六点后要排位！<br />
<br />
65.南罗鼓巷里有一间叫文宇奶酪店。专门卖奶酪，双皮奶，已经杏仁豆腐等~<br />
吃过他家的奶酪双皮奶就不会再想吃三元的了~尤其是红豆双皮奶。超级超级赞。<br />
<br />
66.东四北大街~有一家叫天下第一楼的开封灌汤小笼包。味道超赞~<br />
还有一家叫秦庄的西安小吃。各种面食都很好吃。大家还可以去试试吃那里的油泼扯面~<br />
<br />
67.后海那边元宝胡同里有一家新疆馆子，里面的东西很赞哦。喜欢吃肉的去那里吃超值，肉的份量很足的。<br />
<br />
最好一小份一小份的要，不要按整条买这样划算。<br />
<br />
68.隆福寺北侧，北京钱粮胡同里有一家绝顶好吃的新疆风味餐馆，看上去不是很好，但里面的各种饭菜都是每味，尤其推荐烤脆骨，和拉条子。羊杂汤也是非常好喝哦~<br />
<br />
69.西单翅酷~相信很多北京人都听过吧，那个变态辣烤鸡翅~实在是有够变态！地点么~当然在西单啦~明珠商场东侧的一个小胡同里~门口大红灯笼高高挂！！！（阿波罗网编者注：好像变态的调料有问题。）<br />
<br />
70.猫眼Pizza~美味指数可与必胜客相提并论~并且价格便宜~是学生们选择的好地方~而且室内设计很有情调~桌子和墙上随处可见各种涂鸦~还有大头贴~值得一去哦！</div>

							
							
							
							
