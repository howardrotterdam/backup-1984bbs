属世属神？要割裂开来看吗？<br />
麻烦给个链接，我真的很困惑。 </blockquote></div>　自由国度是怎样崛起的 <br />
　　<br />
　　《自由的崛起――16-18世纪加尔文主义和五个政府的形成》书评 <br />
　　<br />
　　 美国独立宣言开头的序言，就这样宣称： <br />
　　<br />
　　　　“我们认为下面这些真理是不言而喻的：人人被造平等，造物者赋予他们若干不可剥夺的权利，其中包括生命权、自由权和追求幸福的权利。为了保障这些权利，人类才在他们之间建立政府，而政府之正当权力，是经被治理者的同意而产生的。当任何形式的政府对这些目标具破坏作用时，人民便有权力改变或废除它，以建立一个新的政府；其赖以奠基的原则，其组织权力的方式，务使人民认为唯有这样才最可能获得他们的安全和幸福。” <br />
　　　　人人平等，是因为每个人都是上帝根据他自己的形象创造出来的，每个人与上帝，都有直接的关系。在上帝和人之间，不存在任何的中介，也没有任何其他的被造物，可以作为这样的中介。因为所有一切无不在上帝的主权下，除了上帝之外，没有任何人或组织，可以让人类屈膝，可以宣称以另外的依据统治我们。正是上帝，赋予了人不折不扣必须被尊重的权利，并且让人有机会认识到这一点。如加尔文所说，“人总是全然败坏的、自以为公义和圣洁的；除非谦卑地仰望神的面，否则人不可能认识自己”（转引自《加尔文基督教要义》）。 <br />
　　<br />
　　　　人人平等，几乎是近代最响亮的口号。但口号背后，却依托着不同的根基。一种把人的平等建立在人自身的伟大之上，认为人的意志高于万物，这是自然的法则。正如法国大革命所宣言的，既然人的意志是最高的主宰，人的意志就可以汇合，于是数人头就成为政治中的最高原则。同时，既然人的权利，只是他自己的私有财产，也就意味着这权利是可以被转让的，人可以“自由”地把自己卖为一个奴隶。所以，必要时把个人的权利，打包转让给国家或最高领袖，也是自然的事了。以至于那些打着社会契约幌子的共和，最终也无非是把个人的权利，转交给一个或一群人罢了。但另一种平等，是把根基建立在上帝、建立在十字架那那里。人与人的一切差异，都伏在其下，都不再是人间不平等的根据了。从进化论不可能引申出政治哲学上的平等，只有从创世论才能引申出人间的平等，和真正的平等。既然所有人都由上帝创造，那么我之为人的依据，就不在地上。我之为人的权利，就不是私有财产，不是私相授受的，当然也不是任何人或组织所能剥夺的。同时，也是不可转让出售的。既然我的灵魂，直接向上帝负责，那么任何其他人，就都没有支配我思想的权利。 <br />
　　　　当我们谈论权利，谈论平等自由时，请不要忽略一个事实，那就是人的罪性和有限性。罪破坏了我们的良善，使这个世界全然败坏。一个诚实的人，会承认他的心思意念，行事为人，无时无时不在罪的诱惑中，不在贪图着别人的财产、身体、妻子、房屋、名声和一切。这个世界的秩序在本质上是混乱的，就如我们的内心。所以我们无法继续我们的生活，除非有一种新的宇宙秩序、心灵秩序的建立，然后才可能带来一种自由的社会秩序的更新。 <br />
　　这种自由秩序的建立，不是在人的契约论下，把人的权利转让给国家。也不是为一个人或某个组织的利益。而是为每个人身上从上帝而来的形象、尊严和权利，免于他人的破坏。同时阻止人群在公共生活中走向专制。因为人人都有罪性，不存在没有罪性的人，不论总统或任何人，都在这个秩序的管辖之下，都因着遵守这个秩序而得着自由，或自由的可能性。也因为人都有罪性，所以需要法治。国家提供法治，约束人群中的堕落；教会仰望恩典，使人从堕落的本性中得自由。一个管理身体，一个医治灵魂，政教分离，其实也是政教合作。 <br />
　　　　因着律法的强制性，政府就成为必不可少的恶。这恶源于人的堕落。既然人的良心没有自我管理的能力，外在的法治秩序，就把基本的个人自由圈在里面。但为了防止法治秩序侵犯个人的自由，限制这个秩序的能力大小和所调控的范围，也就是宪政主义，就成为必要。政府只能管辖公共领域，不能管辖个人的私密空间；只能管辖人的行为方式，不能管辖人的信仰、思想和言论自由。 <br />
　　　　在一个“有教堂的开放社会”当中，对上帝的敬虔（宗教），才是对个人自由最至关重要的保障。在“我”和上帝的关系中，“我”隐秘地管理我的一切，外在法治秩序，只是保障“我”和上帝的关系能够在地上顺利地表达。在“我”对上帝的崇拜中，我的个体意识时刻都被确认。“我敬拜，故我在”。这种意识也不断提升、激励着“我”以一个公民社会的个体的方式，而活在他人的面前。也因此，一切的自由教育，也必须以信仰的教育为支撑，否则就落入极其荒谬的结局。 <br />
　　　　从神学史来看，在奥古斯丁那儿，对人的罪性和有限性的体认，奠定了他的“双城论”的基础。这既是源于西方悲情哲学的传统――它有些过于希腊化，也是对罗马帝国被毁灭的现实回应。每个人从降生到这个世界上来，就分属于两个大的社会，这两个社会分担了各自的公共责任。政府是法治秩序的提供者，它当保护基督徒的信仰和生活自由不受干涉。基督徒也有顺服政府权柄的义务。但是，国家在本质上是一个执行者，而不是立法者。国家不能决定人应该怎么生活。决定人类生存的终极目的，并指导政府完成其本分的，乃是教会所发挥的正当的社会功能。所有信徒都有义务以自己信仰生活的见证，来帮助教会完成这一社会功能。而不是督促政府将这样的公民社会的功能国有化，进而侵蚀公民社会和信仰自由的健康活力。 <br />
　　　　在加尔文主义或改革宗神学中，上帝荣耀的主权占据着核心位置。世上所有发生或未发生的一切，无不在上帝的主权之下。政府的权力依然来自上帝的委任，否则就是乱政。所以我们基于对上帝的敬畏，也会对政府权柄表示最大的尊重。因为凡是权力，都从上面来的。正如耶稣对罗马总督彼拉多所言的，若没有父赐给你的权力，你什么都不能。我们顺服法律，也顺服法律确认的公民权利，我不是顺服任何人，也不当顺服任何人。但我们顺服符合上帝旨意的法律，也顺服执行这法律的任何人。正义和道德的标准，始终在上帝那里，不在人这里，但人怀着敬畏的心，却可以了解。由此，一切的国家崇拜、政党崇拜和集体崇拜、权威崇拜就被否定了，真正的自由主义在信仰的推动下，才能走向真正意义上的个体觉醒。 <br />
甚至在极端的情况下，世俗统治者若执意不听从教会所宣讲的教导，以致走向对人的权利和尊重的极大的侵犯。有良知的基督徒，甚至应放弃对某个世俗政权的忠诚，担起建立政治新秩序的革命性的责任。这就是苏格兰长老会的历史。这种二元社会观的建立，标志着西方近代文明的确立。在世俗法律无法约束政府权力泛滥的时候，在个人与政府之间，始终有一个道德性的仲裁者，一个先知般的声音，和一个公民社会中的力量主体。那就是由自由的基督徒们所组成的那个社会实体。所以，加尔文所做的就是重新回到、阐释和捍卫基督徒的基于上帝话语的自由。无论是信仰自由，还是社会性的个人自由，都只能在一个“有教堂的开放社会”中，透过从真理到生命、从生命到伦理、从伦理到法治的转变，才会实现。 <br />
　　　　这本书所描述的，就是当代的一个开放社会的形态，如何在作者所描述的五个政体中演变出来的。自由宪政主义的产生为这个社会提供了一个开放的架构，这个架构在本质上继承了奥古斯丁的二元社会观。就连写了《理性时代》的潘恩，也不得不承认“宪法是先于政府而存在的”这一基本的基督教化的立宪观。 <br />
　　　　看看美国宪法的描述，再比较一下圣经中的盟约，我们看到太多的“不要……”，因为这里列举了人常犯的错误，但没有一个人天生是基督徒，或天生是义人，反而在始祖堕落之后，生性为恶。上帝通过旧约的律法，阻止他们随心所欲地行事，使他们心里的恶，不过分表现为行动。新约中的《罗马书》、《加拉太书》，因此确定律法的功效是：教导人认识罪，承认罪，使人谦卑，以至愿意在基督里领受恩惠和信心。同样，人间的宪法和法律里，第一件要告诫人们的事，就是人是有限的，政府更是有限的。政府必须认识、承认自己的界限。因为宪法是先于政府而存在的，而宪法的道德性，是依据上帝的正义而来的。否则，所有的平等、自由都只流于形式，只会导致有宪法而无宪政的局面。 <br />
　　　　新教改革宗（加尔文宗）的自由观，是一种基于《圣经》启示的、国度和圣约之下的自由。凯利（DouglasF.Kelly）的这本书，论述从16世纪到18世纪，这一新教改革的加尔文主义遗产，如何塑造、演变和更新了英美的宪政观，尤其是对政教关系等政治哲学议题的巨大影响。这本书，起因于一个神学家与法学家的联合研讨小组。在宗教与法律的混合视野下，作者论述了加尔文主义在16-18世纪对五个政体（日内瓦、法国、苏格兰、英格兰和美国）的影响，并如何持守和参与演化出这一人类政治自由传统的。凯利也提及了加尔文主义影响的一个世俗化过程，以及自由主义和启蒙运动在这一过程中越来越大的影响力。 <br />
　　　　这对知识分子的一个传统认识构成冲击，就是认为近代的自由政体，是由无神论色彩的文艺复兴和启蒙运动所塑造的。事实上，在加尔文主义影响下的三次革命，荷兰革命、英国革命和美国革命，产生了与法国革命、俄国革命截然不同的后果。法国大革命产生了君主专制和国家主义，而在荷兰、英国和美国，却产生了宪法保障下的个人自由政体。历史很难像显微镜观察微生物那样被描述，我们也许可以说，有加尔文主义不一定就有上述的结果。但我却敢说，没有加尔文主义，就没有当代英美的体制与文明。在国内，每当知识分子提起加尔文时，在我们脑海中首先想到的，可能是他的独裁，他的政教合一的小丑化的形象。但是事实究竟如何，却鲜有人愿意追寻。 <br />
　　　　自由、平等、博爱、宪政、民主，这些美好价值从来不是知识分子喊口号的结果。而是每个人在认识到自己的罪性、有限和无力之后，走向自我克制，相互宽容、接受约束而建立起来的。在西方的政治传统中，持守纯正信仰的基督教会为着信仰自由、政教分离和神的公义，行在地上争战不停。很大程度上，这一股力量与启蒙主义和理性主义的力量一起，塑造了近代文明。但宪政主义的真精神的核心，却从来不是启蒙主义的，而是基督教的一种法治的保守主义精神。自由政体的崛起，与这一精神血肉相连。然而在中国，这样的精神却未曾积累，也不被珍惜，甚至在一百多年学习西方的历程中，反而被人文知识分子们给予轻率的忽视和妖魔化。不能不说这是我们近代以来的一个深重悲剧。 <br />
