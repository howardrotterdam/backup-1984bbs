<br />
但是基督教的宗旨并不是这一些，基督教解决“终极问题”，我的理解就是三个问题：你从哪里来，到哪里去，以及今生的意义是什么。其实这三个问题是关联的，你不知道从哪里来，到哪里去，你就无从知道今生的意义是什么。<br />
<br />
对我而言，这三个问题都是指向上帝。<br />
<br />
我承认我是一个无药可救的罪人，但耶稣基督救赎了我。我信耶稣基督，不仅找到了今生的意义，更有永生的盼望。<br />
<br />
前面贴子说到，为上天堂而信耶稣的“伪”信徒。其实“伪”信徒这样的说法既对也不对。信耶稣，上天堂是一个结果，这是对的；但是信耶稣不是“为了上天堂”。类似的比喻可以这样说：信老爸得遗产。不可否认，有人会为了遗产而信老爸，然而信老爸，回到老爸的怀抱才是真正的意义，而遗产，只不过是一个副产品而已。<br />
<br />
无论是为了遗产还是为了回到老爸的怀抱为目的，信老爸，就是回到老爸家里了。老爸是心胸是大的，你浪子回家，遗产就有你的份。而且在老爸家里待久了，那些仅仅以遗产为目的不肖子，迟早会认识到自己的错误。总有一天，他会说：老爸，我不求别的，只求和你在一起就满足了。<br />
