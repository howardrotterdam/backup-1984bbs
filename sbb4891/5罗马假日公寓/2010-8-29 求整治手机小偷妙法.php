																											<div id="postmessage_809130" class="t_msgfont">凭借串号是不能锁住被盗手机的 <br />
<br />
有网上信息称，熟记自己的手机串号能“治”小偷。 <br />
<br />
网上信息称：所有手机都有一个唯一的串号，用户如果在手机上键入＊＃06＃时，手机屏幕上就会出现一组15位的阿拉伯数字，这就是该部手机的串号。万一手机被盗或丢失，只要用户将串号通知手机服务商（移动公司或联通公司），服务商能将丢失的手机锁住，即使小偷或拾到手机的人换掉原有的SIM卡也不能使用该手机。 <br />
<br />
就此，中国联通南京分公司市场部的一位戴姓工作人员介绍，每部手机确实都有一个串号，该号码不会重复。但手机串号属手机的设置问题，应是手机生产商的业务范围。而联通只是手机的网络运营商，不可能通过网络来控制手机的设置，因此无法将手机串号锁住。江苏移动通信公司南京分公司相关工作人员也给了同样的解答。 <br />
<br />
戴先生同时提醒，一旦手机遗失或被盗，机主应及时向联通或移动报停SIM，防止偷窃者使用卡内余额。同时，机主也可联系公安机关备案，同时告知其串号，可为对方调查提供帮助。 <br />
<br />
由此可见，所谓“凭借串号能锁住被盗手机”的说法是没有根据的！</div>

							
							
							
							
