																											<div id="postmessage_781347" class="t_msgfont">宋石男十七岁就破处了？？？<br />
<br />
请看此博文<br />
<a href="http://www.bullock.cn/blogs/ppn/archives/113770.aspx" target="_blank">http://www.bullock.cn/blogs/ppn/archives/113770.aspx</a><br />
<br />
见宋石男<br />
皮皮娘 @ 2010/6/17 10:55 阅读(354) 评论(2) 推荐值(12) 分类: 自言自语<br />
宋石男来济南做他的处女电视节目，我跑去济南，跟这个号称四川第一交际花见了一面。<br />
<br />
 <br />
<br />
宋石男晚上9点的直播节目，5点多接到南方都市报的足球约稿，我们找了个网吧，他在网吧写的稿子，一个多小时就喷出来了。这个驴子、种马。本地话叫做“苍”，真苍啊。<br />
<br />
 <br />
<br />
在节目化妆间，宋石男也没忘了调戏一下女主持，笑的人家花枝乱颤，后来宋石男告诉我他是怎么沟女的：我问她是不是跟人家打架总是赢，她说为什么这么说，我告诉她你只要瞪人家一眼你就赢了，因为别人一瞪眼，眼光是一束，你一瞪眼，眼光射出去一把。对方立刻败了。<br />
<br />
 <br />
<br />
宋石男写足球稿子，先把想说的话喷出来，然后再调整增删成文。说明此人是先有激情，后有理性。<br />
<br />
 <br />
<br />
做完了节目，晚上吃宵夜，喝啤酒，吹牛逼，从11点吹到第二天3点，济南下着小雨，凉爽的风吹着，我从有了孩子，就没有这样爽过，忽然羡慕自己没结婚的日子了。<br />
<br />
 <br />
<br />
第二天早上，我陪他游了一下济南的趵突泉，看了看李清照故居，四一对趵突泉的园林布局的大气赞不绝口，看到了郭沫若到处留的墨宝，四一有些气恼，却没有多说，看来对这个乐山老乡还留一点面子。<br />
<br />
 <br />
<br />
一边游玩，四一一边跟我介绍山东的文人，如数家珍，说山东清朝文气的没落。弄得我这个本地人只有听的份儿。感觉山东的文化的发扬以后要靠我了。<br />
<br />
 <br />
<br />
时间太紧，大明湖没有进去游玩，在湖边依依不舍的溜达了一段，却意外的发现了辛弃疾纪念馆。纪念馆旁边的碑文说这原是李鸿章的生祠，后改成辛弃疾纪念馆，牌匾上居然有唐伯虎的题字。唐伯虎，辛弃疾，李鸿章。这三个名字放一起。很纠结。山东真的没文化人了？<br />
<br />
 <br />
<br />
一路上，四一又跟我说起他的偶像辛弃疾的生平，我就记住了一个故事，就是辛弃疾本是一个北边一个游击队长身边的秘书，后来头儿被一个和尚出卖，辛弃疾带人冲入敌营，把和尚绑了出来，宰了。提着头（和尚怎么提？）到南方。其精彩程度堪比海豹突击队的斩首行动。<br />
<br />
当四一讲到辛弃疾晚年贪腐，“花钱如草”的时候，中午时间到了。我们钻到大明湖边的居民区品尝了一个青岛大厨的家常山东菜。<br />
<br />
 <br />
<br />
然后四一送我到车站。他飞回成都。<br />
<br />
 <br />
<br />
宋石男比我小，每次回想起自己曾经一口一个四一哥，我都会内心非常羞愧，然后我心中会有一个自己安慰自己说，朋友在一起不比这个。然后自己又讥讽自己说，看看人家小你那么多。你自己傻逼呼呼的不知道羞耻么？最后我就变得很纠结。后来我决定不再想这事儿。<br />
<br />
 <br />
<font color="red">跟宋石男扯起我29岁才破处那事儿，我跟四一哥说，我相信爱情，四一贼不理解（似乎在他来看，爱情和性是完全的两码事儿），炫耀自己17就破了，他问我现在还相信么，我说还相信，这话说得如此的底气不足，有时候我自己都觉着自己不相信爱情。</font>其实，爱情是这样的东西：把对方的幸福看得比自己的幸福重要。从这个意义上说，我比四一博爱的多。这也能解释我的身体上忠贞，因为我爱的人在乎。――这算代沟么？还是地域差异？我不清楚。<br />
<br />
 <br />
<br />
其实，我很小心的保持着与宋石男的距离，写小说的人太毒，我不敢不防。遇到模棱两个的意思，我宁肯先否认，然后再解释，宋石男很敏锐的问我，你为什么总是说“不”啊？<br />
<br />
 <br />
<br />
一共吃了3顿饭，有两次是四一结账。是为记。<br />
<br />
 <br />
