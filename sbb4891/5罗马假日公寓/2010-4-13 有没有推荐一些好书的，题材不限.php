一份书单（二）　　文/梁由之<br />
<br />
2008-05-31 10:45:57 来自: 左手(不着急，慢慢来)<br />
<br />
<br />
　　小记：网友海边之人兄的女儿在厦门大学中文系读书。她们看了我的《一份书单》，认为还不错，有一定参考价值；并进而要求我针对小朋友的专业特点再开一份书单，即偏于文学方面。推辞不掉，恭敬不如从命。当时比较忙，我承诺在本月初开出。鉴于也有一些朋友先后提出过类似要求，经海边之人兄同意，我将这份书单在书话贴出，供有兴趣的朋友一笑。为了避免重复，《一份书单》中已经列入的书籍概不涉及。 <br />
　　 <br />
　　1.《远东英汉大词典》（梁实秋主编，远东图书公司）。读书必先识字。当今时势，只识汉字是远远不够的。这本词典我用了多年，觉得还好使。牛津词典很常见，故不及。 <br />
　　2.《庄子释译》（欧阳景贤等，湖北人民）。千古奇书，百读不厌。文字的优美恣肆，意义的婉曲精深，妙不可言。这个本子不错，但印刷相当差劲。 <br />
　　3.《诗经译注》（程俊英，上海古籍）。很好的本子。 <br />
　　4.《白话楚辞》（吴广平，岳麓书社）。不错的本子，尤便初学。 <br />
　　5.《史记》（中华书局）。当文学作品看一样好，甚至更好。必须精读。 <br />
　　6.《世说新语译注》（张万起等，中华书局）。也许是中国历史上最有情致的一本书。如果做研究，当然还是余嘉锡先生的本子好。 <br />
　　7.《唐诗鉴赏词典》（萧涤非等，上海辞书）。同类书中较好的一种。后出的，一蟹不如一蟹。 <br />
　　8.《今古奇观》（人民文学）。除了文采，还可以见识到古代尤其是明朝中期的风土人情，很有味道。可读性亦强。 <br />
　　9.《聊斋志异》（河北教育）。这是一本奇书，百读不厌。我读书取实用主义，一般不太讲究版本。此书，因为太过喜欢，买有多种版本，各有所长。上列是白文本口袋书，轻巧漂亮。 <br />
　　10.《人间词话百年解评》（刘锋杰等，黄山书社）。这个本子资料比较丰富。以我的偏见，王静安薄薄的一本《人间词话》，水平是在钱钟书的大作《谈艺录》之上的。其文字之精洁，见解之高超，梁某叹为观止。 <br />
　　11.《朝花夕拾》（鲁迅，人民文学）。比较适合现阶段阅读。不忙读《野草》。 <br />
　　12.《鲁迅书信集》（上、下册，人民文学）。可以学到很多东西。对准确、深入认识和了解鲁迅也很有帮助。 <br />
　　13.《沈从文散文选》（湖南人民）。收有从文自传、湘行散记、湘西、一个传奇的本事。除了《边城》，沈先生的精华大抵集于此书。 <br />
　　14.《书林秋草》（孙犁，三联）。内容和版式都很不错。现在很少见到这样平实娟秀的好书了。 <br />
　　15.《黄裳散文》（浙江文艺）。黄裳的文章、见识都不错，我很喜欢。而其各种集子收文之一再重复，也颇为人所诟病。斟酌了一下，还是推荐这个本子吧。 <br />
　　16.《锯齿啮痕录》（流沙河，三联）。同样是写右派生涯，窃以为流沙河的&lt;&lt;锯齿啮痕录&gt;&gt;比&lt;&lt;往事并不如烟&gt;&gt;要好，主要在于两点：其一，没有戾气。其二，没有洗刷不去的优越感。平实深婉，感人至深。这是一部不甚知名的大家小书，几乎绝版。我的一本三联初版书，被一个老友有借无还。去年，这位老兄去了法兰克福，随身携往约30来本中文书籍，该书荣幸入选。回归无望，再版无期，也许只有网上淘书一途了。 <br />
　　流沙河“派曾右”后，拉过好几年大锯，两人对拉，锯木板。其诗云：“六十四颗铁钉啃着我的中年。”当时感觉惊心动魄。久而难忘。 <br />
　　17.《红楼梦与百年中国》（刘梦溪，河北教育）。内容丰富，立场公正，对红楼梦及红学作了鸟瞰式勾勒与回顾，值得一读。 <br />
　　18.若干值得认真一读的现当、代小说： <br />
　　阿Q正传,故乡，在酒楼上，孤独者，伤逝(鲁迅) <br />
　　呼兰河传，小城三月(萧红) <br />
　　边城(沈从文) <br />
　　铁木前传(孙犁) <br />
　　老井(郑义) <br />
　　草青青、青砖的楼房(何士光) <br />
　　叔叔的故事(王安忆) <br />
　　风景、桃花灿烂（方方） <br />
　　祭奠星座、绝望中诞生(朱苏进) <br />
　　重瞳、对话(潘军) <br />
　　黑骏马、北方的河（张承志） <br />
　　来到人间、命若琴弦、我与地坛（后一种是散文，史铁生） <br />
　　晚霞消失的时候（礼平） <br />
　　金瓯缺（徐兴业） <br />
　　马桥词典（韩少功） <br />
　　遗弃(薛忆沩)...... <br />
　　19.《中国现代小说史》（杨义，人民文学）。好的《中国文学史》尚未出世。此书由杨义独立完成，很见功力，亦算难能可贵了。 <br />
　　20.《喧哗与骚动》（福克纳，李文俊译，浙江文艺）。结构谨严，描写精确，意识流动，大家之作。 <br />
　　21.《百年孤独》。不多说。地球人都知道。 <br />
　　22.《呼啸山庄》（艾米莉.勃朗特，方平译，上海译文）。一部表面简单、其实内涵相当复杂的小说。多年前初读时的震撼记忆犹新。 <br />
　　23.《妇女乐园》（左拉，侍桁译，上海译文）。著译俱佳。与中国当今现实很具可比性。 <br />
　　24.《金阁寺.潮骚》（三岛由纪夫，唐月梅译，译林）。极具张力与异质的文字。只是这个译本不是很理想。 <br />
　　25.《罪与罚》（陀思妥耶夫斯基，朱海观等译，人民文学）。长篇名家中，只有陀氏的著作全部看过，个人最偏爱这一部。杜尼娅是我心目中最有光彩的俄罗斯少女。我认为，陀氏写作此书时，精神最为健全。 <br />
　　26.《加缪传》（洛特曼，肖云上等译，漓江）。一个文学史与思想史上奇才的生平和精神传记。折射了一个时代。对深入了解存在主义群英也很有助益。 <br />
　　 <br />
　　该打住了。还是留待以后与朋友们一起探讨补充吧。 <br />
