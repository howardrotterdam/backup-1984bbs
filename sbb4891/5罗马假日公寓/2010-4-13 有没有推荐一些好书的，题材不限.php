作者：梁由之 分类:书边杂俎 提交日期：2006-6-28 21:01:00 浏览:2771 <br />
　 <br />
　　小引 <br />
　　 <br />
　　早就有朋友向我鼓吹过网上购书的种种妙处。但一直言者谆谆，听者藐藐。人都有惰性，接受新生事物需要某种契机和一个过程。 <br />
　　作为闲闲书话的常客，偶尔也到隔壁的天涯书局逛逛。2005年3月的一天，看到厦门的一家书店开帖售书，列有黄裳的《来燕榭读书记》。梁某读书，眼光是比较挑剔的。天海楼设有专柜的中外名家，也就寥寥十数人而已，其中之一是黄裳。喜欢老头的文字，垂20年，迄今未曾厌倦，也算是一个难得的异数了。特别欣赏他的书话，想当然以为这套书也是。全新品相，价格却低至4折。这等好事，不“试水”而何？ <br />
　　这是我在网上买的第一种书。款去书来。一切就这么简单。 <br />
　　两个月后，又在南京一家书店邮购了一套《耕堂劫后十种》。 <br />
　　试水的感觉真是不错。就此大开杀戒，一发不可收。一年多时间，网上购书早已超过百种千元。 <br />
　　书款安全。汇款也并不如预想的麻烦：开始在邮局寄，还有那么一点；后来直接用银行卡转帐，就非常便利而且减省了。书的质量、品相有保证。价格可以接受才下单，有的相当划算。有选择余地。寄书及时。尤为重要的是：几乎可以买到你想买的任何书籍。身边实体书店一下子变得不是那么重要了，光顾的次数和频率明显减少。这等好事，非网络时代，不可想象。 <br />
　　网上购书，以孔网为主，当当、卓越、天涯书局等为辅。 <br />
　　有些心得和感触。若干网购书籍也着实值得闲闲地话它一番。趁此看球间隙，开出《第三份书单》，专谈网上购书，并将持续更新。 <br />
　　今天又适逢梁某在天涯、同时也是在网上的第一张帖子《一份书单》发出一周年。在这个日子发出《第三份书单》，兼寓对在书话惠我良多的各位网友的一份敬意和感谢。孔网是个好地方。书话也是。（待续）<br />
