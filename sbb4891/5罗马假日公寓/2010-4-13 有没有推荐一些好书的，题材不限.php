两份书单（梁由之）<br />
1.《现代汉语词典》(商务印书馆)。读书必先识字。<br />
2.《唐宋名家词选》(龙榆生编选，上海古籍)。最好的本子。<br />
3.《唐诗三百首直解》(浙江古籍)。很好的本子。没有一点唐诗宋词打底子的中土学人肯定是撇脚的。<br />
4.《史记选注集评》(韩兆琦，广西师大）。内容精要，颇便初学。<br />
5.《三案始末》（温功义，重庆）。不知名的大家小书，比新出的那些大部头的明史著述强出不知凡几。尤其要精读《帝权与相权》、《从宰相到阁臣》这两章，并注意其与民国初年总统制与内阁制之争的内在关系。作者是《大公报》旧人，曾亲炙张季鸾、胡政之两先生。<br />
6.《红楼梦人物论》（王昆仑，三联）。立论通达，文采斐然。<br />
7.《中国自助游》（中国友谊出版公司）。同类书中最好的。很不错的导游和工具。读万卷书，还得行万里路。即就一个读本而言，也是很有意思的。<br />
8.《变化*1990--2002中国实录》（凌志军，中国社科）。此书有三最：胡舒立、凌志军、卢跃*、余刘文，是我心目中中国现在最优秀的记者；本书是迄今为止凌最好的著述；在现时语境下述评这段非常时期，我认为本书已尽最大努力。<br />
9.《黄河边的中国》（曹锦清，上海文艺）。一味躲在书斋里革不了命，也难做好学问。<br />
10.《近二十年文化热点人物述评》（骆玉明，复旦大学）。剀切工稳。可惜漏列三王（蒙，朔，小波）。<br />
11.《汪曾祺作品自选集》（漓江）。多好一老头。<br />
12.《沉默的大多数》（王小波，中国青年）。多好一哥们。<br />
13.《品人录》（易中天，上海文艺）。他的语言具有一种特质。言必有据，但毫无书院气，象好的侦探或武侠小说一样好读。<br />
14.《沉重的肉身》（刘小枫，上海人民）。刘的思想，与我南辕北辙。但读他的东西，还是有一种被冒犯感(借用王小波的说法)。<br />
15.《文化人与钱》（陈明远，百花文艺）。鲁迅说：梦是好的；否则，钱是重要的。胡适晚年多次劝唐德刚趁早多攒些钱。老舍说：钱是人的胆子。该书对文化人在当今时世如何安身立命会有借鉴作用。<br />
16.《中国传媒》（孙际铁，珠海）。读之可对时下传媒业有个大致的了解。<br />
17.《大公报史*1902--1949》（周雨，江苏古籍）。1949年之前的&lt;&lt;大公报&gt;&gt;是百年中国最成功的报纸，张季鸾先生是中国最杰出的报人。顺便说一句，张氏传人中，我欣赏徐铸成，而不怎么喜欢王芸生。<br />
18.《揭开真相--南方周末知名记者报道手册》（谢春雷，浙江人民）。可作为采访、写稿的向导书。<br />
19.《世纪的电影》（吴冠平主编，三联）。可指导看片和淘碟。<br />
20.《西方著名哲学家评传*第八卷》（山东人民）。该书对我十多年前的思想建构有举足轻重的影响。它也会指导你去读一些真正值得读的西方经典著作。<br />
21.《哲学研究》（维特根斯坦，商务）。如果愿意想得深些、玄些、形而上些，该书是一不错的选择。<br />
22.《形而上学》（泰勒，上海译文）。名不副实。其实很有意思，也很好读。<br />
23.《非理性的人》（巴雷特，商务）。一条一以贯之的西方思想文化主线。<br />
24.三部中篇小说《变形记》（卡夫卡），《局外人》（加缪），《老人与海》（海明威）。见多种集子。由此可见文学的深度与力度。你随后也就知道了精华与垃圾的区分。<br />
25.《宇宙的最后三分钟》（戴维斯，上海科技）。比《时间简史》好读得多。</div>

							
							
							
							
