最初学日语的时候是因为那时候喜欢听日语歌, 但把假名背下来之後发现日语是个很有趣的语言, 买了两本字典一有时间就随机翻开看几个词, 把歌词都打印出来看着听歌. 还上网看了许多日语的历史演变. 总之不知不觉就学会了不少东西. 比如日语一些词汇和古汉语的联系, 汉语和日语之间的&quot;同形异义词&quot;, 日语汉字的读音规律, 等等. 虽然不像按着教科书一课一课学那样系统, 但看个产品说明书这样简短的东西还是基本能看懂的. 後来大学日语选修课分I和II我直接选了II, 不用怎么学, 考试分比其他同学都高, 因为其他人感到记忆困难的汉字读音对我来说完全不是问题. 全是兴趣使然. <br />
