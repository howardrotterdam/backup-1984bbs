衣鱼虽然对人类生活造成滋扰，但其实是无害的。 在建筑物里，衣鱼必须要有潮湿及有空隙的环境才能生存；只要环境干燥、建筑物没有裂缝，衣鱼就会自然消失。以下一些扑灭或消除衣鱼的方法，虽然有效，但都是治标不治本的：<br />
混合比例为1:1的硼砂和砂糖，能有效杀除衣鱼。<br />
氯化铵水的气味应该能于24小时内驱赶衣鱼。<br />
若要捕捉衣鱼，将石膏粉洒在浸湿的白棉布上，隔夜放在房间一角，放的地方要接近衣鱼的藏身处。<br />
以下方法同样有用：可以在衣鱼藏身处旁边放一块木板，板上再放一颗稍微磨碎的马铃薯；衣鱼晚上出没时就会钻进马铃薯里面大快朵颐，次天早上，你就可以把马铃薯连同衣鱼一起丢掉。<br />
使用樟脑丸(moth ball)，又称奈丸(Naphthalene ball)可以让衣鱼不敢靠近。</div>

							
							
							
							
