																											<div id="postmessage_822042" class="t_msgfont">开始之前务必记住： <br />
<br />
　　黄金法则：80/20---你要承担起80%的谈话而面试官只会说20%。 <br />
<br />
　　白金法则：你必须试着控制面试的节奏和话题。 <br />
<br />
　　钻石法则：对于没有把握的问题，抛回给面试官。 <br />
<br />
　　1．在一分钟内介绍一下你自己 <br />
<br />
　　这似乎是必答题。不要以为这很容易。如果你用一分钟来重复你的简历，恭喜你，你的印象加分没有了！建议你最多用二十秒钟介绍自己的姓名、学校、专业。然后话锋一转，引出自己的优势或强项。一定要在最短时间内激发起面试官对你的好感，或者至少是兴趣。 <br />
<br />
　　成功的模式可以是：我叫XXX，英文名字XXX，XX省XX市人，今年6月将从XX学校XX专业本科（专科）毕业。除了简历上您看到的介绍，我愿意特别说一下我在XXX方面的特长/我最大的特点是……（给出事例）。正是基于对自己这方面的自信，使我有勇气来应聘贵公司的XXX这一职位。（看表）一分钟到了，希望我没有超时。（很阳光的微笑） <br />
<br />
　　如果面试官不是EQ太低，你的最后一句话应该会使他放松和微笑。资历嫩一点的还会接着问：“为什么你会这么认为呢？”如果他真的这么问你，Bingo!你完全有机会操纵这次面试！ <br />
<br />
　　2．应届生经常会被问到的一个问题是：你为什么会选择你目前学习的专业呢？千万当心，这个问题的目的是考察你的DecisionQuality这一项胜任力，所以不要简单的说“感兴趣”或者“就业前景乐观”等。给大家一个成功的范例（同样适用于诸如‘您所做过的一个成功/最大的决定是什么”）： <br />
<br />
　　问：张先生，您为什么会选择财务专业呢？ <br />
<br />
　　答：的确，财务已经连续多年成为高校热门专业，这造成了就业时无可避免的激烈竞争。可当初我选择财务专业时并不是单纯因为它的热门程度。我早就把就业目标锁定在苏州工业园区的外资企业。根据我高三时搜集到的统计资料，园区当时有外企XXX家，而且每年以XXX%的速度在增加。以每个公司财务部至少5个人计算（总账，应收应付，税务，出纳再加上一个经理），加上园区的平均离职率是15%，则在我毕业时，可以有XXX个空缺。我毕业那年应该全国有XX相关专业的毕业生。其中可能有10%会瞄准苏州，而我填报的苏州大学在当地口碑尚可，属于中等偏上。那么，有1/2的对手能被我淘汰。再加上我就在苏州本地学习，四年中可以更早的寻求机会，所以，我很有信心的选择了这个专业。 <br />
<br />
　　其实，没有哪个面试官会相信你真的作过如此缜密的调查分析，但你已经展示了你做决策时的思路，所以可以加分。 <br />
<br />
　　记住：所有的回答要符合你专业的特点。不要说得太到位，可以自圆其说就行了，不然，他们会觉得你要么太虚伪，要么太狂妄。还有，应该表示对自己的专业的确感兴趣，或者增加点戏剧效果：我本来理解的财务管理就是管账，所以开始的时候还真后悔了一阵，直到大三时开始了在企业的实践，才有了改观，并真正喜欢上了我的专业。这样显得真实可信，更重要的是，很自然的由你引导到准备好的问题：实习的收获。 <br />
<br />
　　3．为了考察您LearningontheFly这一条胜任力，通常我们会问您在实习期间的收获。此时，不要夸大自己的成绩，谦虚一点。还有，不妨说一下自己的失误（不用怕，毕竟你是在实习）。记住：详细说明当时的情况（Situation），你要达到的目的（Task），你采取了哪些步骤（Actions），事情的结果（Result），还有你得到的经验教训(Lessonlearned)以及后来怎样运用到工作中避免犯类似的错误。最后做出总结：原来书本上的知识要能够在工作中熟练运用，这期间还有很长的一段路要走。还可以说：回到学校后，我对自己的实习经历作了一番总结，发现自己在XXX方面还需要加强。所以，我很注意利用大学的最后一个学期来弥补这一不足。现在，我对自己很有信心，如果时间能够倒流，我相信我能做到更好。 <br />
<br />
　　4．你有过和别人合作的经历吗？（千万要回答“有”）那么，在这过程中，你是如何处理意见分歧的呢？现在是考察你的ConflictsManagement。现在的绝大部分企业都不欣赏没有原则的老好人。所以，你要把自己包装得强势一些。 <br />
<br />
　　我本人比较满意的回答： <br />
<br />
　　每个人在团队中都应该可以自由坦诚地发表意见，我会非常认真的聆听，分析；但对于自己的意见我不会没有原则的轻易放弃。民主过后还需要集中。我是学校英语俱乐部的主席，在组织校际年度联欢时，有两个干事的意见和我不一致。（停顿一下，让面试官记住你的这个闪光点）我和他们开了会。大家都阐述了各自的理由。很遗憾，我仍然没有说服这两人。在这种情况下，我感谢他们的积极参与，但表示仍然会采用我的方案。我的理念是：Meeting不等于Voting，完全不需要少数服从多数；我是负责人，我相信自己有能力采取最佳方案；假如失败了，我也会承担主要责任。而如果我是团队的普通一员，我会保留自己的意见，但还是认真执行管理者已经做出的决策。当然，学校毕竟不同于公司，情况会更加复杂，但我坚信，只要遵循“对事不对人”的原则，任何问题都可以得到解决。 <br />
<br />
　　在回答时，一定要眼睛看着面试官，微笑，以冲淡你的咄咄逼人。 <br />
<br />
　　如果你实在没有把握，可以把问题抛还给面试官，试举一例（还是同样的问题）： <br />
<br />
　　问：你是如何处理意见分歧的呢？ <br />
<br />
　　答：您问的恰好也是我最困惑的一点，而学校里老师从来不给我们这样的指点。一方面，我不想做没有原则的老好人，另一方面，大家都是朝夕相处的同学，我不想让他们觉得我盛气凌人。我当时是这样做的：……可一直到现在，我都不知道是否作的正确，也许我可以从您那里得到一些指教，您说我当时这么做有问题吗？ <br />
<br />
　　如果对方马上对你言传身教，那他铁定是菜鸟，你不用紧张了，因为，合理的反应应该是不置可否的说：其实这个问题永远不会有标准答案。 <br />
<br />
　　5．有人问起多个问题同时出现时，应该如何解决。这就是PrioritySetting的胜任力。应对思路是把所有要处理的事情按轻重缓急分成4个象限：重要而且紧急；重要但不紧急；不重要但紧急；不重要也不紧急。但记住，不要直接叙述这个思路，这会让人觉得你不像应届生。还是通过举例说明比较好。我面试过的一个比较好的例子：我在大三时正好要复习准备英语6级考试，这时有机会可以去一家企业做兼职部门助理，但需要每周花三个半天。同时，每月一次的英语沙龙活动要组织安排，还要(不好意思地说)抽空陪陪女朋友（最后一点很重要，可以活跃气氛，还为下面制造了一个不重要不紧急的事例）。我当时利用没有课程的下午去公司工作，一般要到5点半回学校。在公车上的30分钟正好用来总结当天在公司的收获和需要了解的知识。在食堂与女朋友吃晚饭，六点半去教室自习，到十点回宿舍。英语沙龙的工作委派给大二的两个干事，他们每天会到自习教室找我谈10分钟，我会给他们一些建议。这样，我没有花太多的精力在沙龙活动上，可对整个过程都有了解和掌控。后来6级考试顺利通过，那家公司对我的工作评价很高，我也学了很多东西；英语沙龙的活动如期举行，我和女朋友也没有疏远。 <br />
<br />
　　这同时展示了三条胜任力：prioritysetting；delegation和work/lifebalance <br />
<br />
　　作为应届生，在面试前，应该了解一下外企的部门架构。以欧美企业为例，一般部门内的职位从低到高依次为：助理（如果是本科生，有时候可以跳过）---专员---资深专员---主管---资深主管---部门副经理/SectionManager---部门经理。一般，主管要求有5年以上的相关工作经验，这是一个分水岭。好，回过来谈谈面试时如何回答关于职业生涯规划问题。其实,这种问题并不需要你回答得无懈可击,这也办不到---你说得通俗了,认为你胸无大志;回答太专业了,又觉得你好高鹜远. <br />
<br />
　　回答这类问题，有以下几点可以帮你加分： <br />
<br />
　　1．设定一个与自己专业相关的长远目标；这个目标要和公司的工作有关但不要局限在企业内部（因为空间有限，会遭遇许多太过于细节的问题）。例如，HR专业的学生可以说，自己的目标是在35岁之前，也就是10年内，成为一名优秀的人才测评专家或者资深企业人力资源顾问；工程技术专业的学生，可以成为精益生产专家或者黑带大师；IT专业则可以在信息资源整合和ERP解决方案方面成为专家，等等。注意，要着重在你想做些什么，而不是你想爬到什么级别。 <br />
<br />
　　2．把这个目标分解，以1年，3年，5年，10年的进度，逐步推进。这个就要靠你自己编了，恕在下不能一一举例。记住，每个阶段都要说明你能为公司做出怎样的贡献，你能得到怎样的提高，这与你长远目标的关系在哪里。此时，可以适当联系到刚刚提到的部门架构。 <br />
<br />
　　3．在此过程中，除了自己想办法不断充电，还要说一下你希望公司可以给你怎样的帮助。比如能够有岗位轮换的机会；或者能够参与各种项目等。不要提希望公司会给你培训，送你出国，给你报销学费，要表现得自己希望在实践中成长。 <br />
<br />
　　4．强调自己的稳定的心态。你可以这样说：“中国人最讲究‘名正言顺’或者‘不在其位，不谋其职’，所以很多人都认为，只有给了主管的职位，才能运用主管的权利，发挥主管的作用。我的理念和别人不同，我认为恰恰相反，当你展示出了主管该具有的能力，能完成更多的工作，公司自然会考虑对你的职位进行调整。在没有足够的权力时，要使用自己的影响力，所以我鄙视那些成天想着晋升却没有任何建树的人。”这样子，你的自我包装就成功了一大半。 <br />
<br />
　　5．最后要注意，在回答类似问题时，不要显得太胸有成竹。偶尔显示出涉世未深可以让面试官觉得你还有学生的单纯。“这个问题很大，我在学校里也曾经断断续续的考虑过，现在我简单的讲一下，可能会显得一厢情愿，希望得到您的指教”这样的一个缓冲在许多时候很管用哦。（还记得钻石法则吗？） <br />
<br />
　　最后想提一下面试的肢体语言。我面试过的应届生应该没有1000也有800了吧。我发现几乎90%的人在入座后都采用一种非常闭合的姿势，很拘谨。这不能说是坏事，但的确会让面试开始的破冰显得艰难。现在，闭上眼睛，想象一对亲密情侣在烛光下甜蜜交谈，他们的姿势应该是几乎一样的；你们平时和好朋友促膝长谈，相信采用的也是相同的姿势；电视上的国家元首交谈时，绝大多数情况两人的姿势也相同或接近。这是因为：两个人如果在交谈过程中采用同一种姿势，则双方在潜意识中会有彼此认同的感觉。我们把这样的姿势一致称为Rapport。我本人非常注重采用Rapport来增进沟通效果（特别是，如果有人盛气凌人的叉腰对我说话，不论他的职位高低，我也会叉腰和他说话---输人不输阵：）） <br />
<br />
　　还有，面试时，手势的运用很重要。特别是回答较大的问题时，用手虚拟的在桌面上列出1，2，3，这样会让人觉得你很有条理，而且不会显得很稚嫩。而说到我会怎样怎样时，把手掌放在心口，真的有强化的效果。 <br />
<br />
　　一句话：面试时，要表现的有教养，容易<br />
