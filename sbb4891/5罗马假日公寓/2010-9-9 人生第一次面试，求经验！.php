																											<div id="postmessage_820948" class="t_msgfont">工作帮――应聘时，教你听懂面试官问题背后的潜台词！<br />
from 每日鲜果精选 by <a href="mailto:zaolee@foxmail.com">zaolee@foxmail.com</a> (胸不平的姑娘)<br />
100+ people liked this<br />
Shared by William Long<br />
<br />
以应聘外企为例，下面总结归纳了应聘外企面试时一般会提的10个问题，同时附上我们认为比较好的回答，或者说是我们希望你能这样来回答比较好，希望对大家有所帮助：<br />
<br />
1、请介绍一下你自己。<br />
<br />
这是外企常问的问题。一般人回答这个问题过于平常，只说姓名、年龄、爱好、工作经验，这些在简历上都有，其实，外企最希望知道的是求职者能否胜任工作，包括：最强的技能、最深入研究的知识领域、个性中最积极的部分、做过的最成功的事，主要的成就等，这些都可以和学习无关，也可以和学习有关，但要突出积极的个性和做事的能力，说得合情合理外企才会相信。外企很重视一个人的礼貌，求职者要尊重考官，在回答每个问题之后都说一句“谢谢”。外企喜欢有礼貌的求职者。<br />
<br />
2、在学校你最不喜欢的课程是什么？为什么？<br />
<br />
这个问题外企不希望求职者直接回答“数学”、“体育”之类的具体课程，如果直接回答还说明了理由，不仅代表求职者对这个学科不感兴趣，可能还代表将来也会对要完成的某些工作没有兴趣。这个问题外企招聘者最想从求职者口里听到：我可能对个别科目不是特别感兴趣，但是正因为这样，我会花更多的时间去学习这门课程，通过学习对原本不感兴趣的科目也开始有了兴趣，对于本来就有兴趣的科目我自然学习得更认真，所以各门课的成绩较为平衡。通过这样的问题，外企可以找到对任何事情都很感兴趣的求职者。<br />
<br />
3、说说你最大的优缺点？<br />
<br />
这个问题外企问的概率很大，通常不希望听到直接回答的缺点是什么等，如果求职者说自己小心眼、爱忌妒人、非常懒、脾气大、工作效率低，外企肯定不会录用你。外企喜欢求职者从自己的优点说起，中间加一些小缺点，最后再把问题转回到优点上，突出优点的部分。外企喜欢聪明的求职者。<br />
<br />
4、你认为你在学校属于好学生吗？<br />
<br />
外企的招聘者很精明，问这个问题可以试探出很多问题：如果求职者学习成绩好，就会说：“是的，我的成绩很好，所有的成绩都很优异。当然，判断一个学生是不是好学生有很多标准，在学校期间我认为成绩是重要的，其他方面包括思想道德、实践经验、团队精神、沟通能力也都是很重要的，我在这些方面也做得很好，应该说我是一个全面发展的学生。”如果求职者成绩不尽理想，便会说：“我认为是不是一个好学生的标准是多元化的，我的学习成绩还可以，在其他方面我的表现也很突出，比如我去很多地方实习过，我很喜欢在快节奏和压力下工作，我在学生会组织过××活动，锻炼了我的团队合作精神和组织能力。”有经验的招聘者一听就会明白，外企喜欢诚实的求职者。<br />
<br />
5、说说你的家庭。<br />
<br />
外企面试时询问家庭问题不是非要知道求职者家庭的情况，探究隐私，外企不喜欢探究个人隐私，而是要了解家庭背景对求职者的塑造和影响。外企希望听到的重点也在于家庭对求职者的积极影响。外企最喜欢听到的是：我很爱我的家庭！我的家庭一向很和睦，虽然我的父亲和母亲都是普通人，但是从小，我就看到我父亲起早贪黑，每天工作特别勤劳，他的行动无形中培养了我认真负责的态度和勤劳的精神。我母亲为人善良，对人热情，特别乐于助人，所以在单位人缘很好，她的一言一行也一直在教导我做人的道理。外企相信，和睦的家庭关系对一个人的成长有潜移默化的影响。<br />
<br />
6、说说你对行业、技术发展趋势的看法？<br />
<br />
外企对这个问题很感兴趣，只有有备而来的求职者能够过关。求职者可以直接在网上查找对你所申请的行业部门的信息，只有深入了解才能产生独特的见解。外企认为最聪明的求职者是对所面试的公司预先了解很多，包括公司各个部门，发展情况，在面试回答问题的时候可以提到所了解的情况，外企欢迎进入企业的人是“知己”，而不是“盲人”。<br />
<br />
7、就你申请的这个职位，你认为你还欠缺什么？<br />
<br />
外企喜欢问求职者弱点，但精明的求职者一般不直接回答。他们希望看到这样的求职者：继续重复自己的优势，然后说：“对于这个职位和我的能力来说，我相信自己是可以胜任的，只是缺乏经验，这个问题我想我可以进入公司以后以最短的时间来解决，我的学习能力很强，我相信可以很快融入公司的企业文化，进入工作状态。”外企喜欢能够巧妙地躲过难题的求职者。<br />
<br />
8、你期望的工资是多少？<br />
<br />
外企的工资水平是很灵活的，何种能力拿何种工资。外企喜欢直率的人，但这个问题却不能正面回答，外企希望听到：“以我的能力和我的优势，我完全可以胜任这个职位，我相信我可以做得很好。但是贵公司对这个职位的描述不是很具体，我想还可以延后再讨论”。外企欢迎求职者给其定薪的自由度，而不是咬准一个价码。<br />
<br />
9、你能给公司带来什么？<br />
<br />
外企很想知道未来的员工能为企业做什么，求职者应再次重复自己的优势，然后说：“就我的能力，我可以做一个优秀的员工在组织中发挥能力，给组织带来高效率和更多的收益”。外企喜欢求职者就申请的职位表明自己的能力，比如申请营销之类的职位，可以说：“我可以开发大量的新客户，同时，对老客户做更全面周到的服务，开发老客户的新需求和消费。”等等。<br />
<br />
10、你还有什么问题吗？<br />
<br />
外企的这个问题看上去可有可无，其实很关键，外企不喜欢说“没有问题”的人，因为其很注重员工的个性和创新能力。外企不喜欢求职者问个人福利之类的问题，如果有人这样问：贵公司对新入公司的员工有没有什么培训项目，我可以参加吗？或者说贵公司的晋升机制是什么样的？外企将很欢迎，因为体现出你对学习的热情和对公司的忠诚度以及你的上进心。</div>

							
							
							
							
