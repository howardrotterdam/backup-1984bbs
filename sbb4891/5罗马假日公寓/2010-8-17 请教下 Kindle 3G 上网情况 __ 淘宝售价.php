																											<div id="postmessage_787559" class="t_msgfont">Bambook or not Bambook<br />
from 狂人山庄 | Silence,声仔,吴洪声,奶罩 by 吴洪声<br />
2 people liked this<br />
<br />
在我悲剧的bambook邀请码申请之路和各种走后门找关系的磨难后，我终于拿到了多灾多难的Bambook。<br />
<br />
在试用两天后，感觉没有预期的好。如果要让我来为bambook定价，并且把bambook定位为电子书的话，我的心理价位是500左右，币种是人民币。<br />
<br />
首先说说bambook的优点吧：<br />
<br />
&nbsp; &nbsp;1. 省电。当然这要得益于E Ink的屏幕。根据官方参数是24天的待机。但在新手入门里面提到的是19天还是多少了。不过具体看书能看多久还需要等待测试。<br />
&nbsp; &nbsp;2. 轻薄，282克，比一本小说要轻。当然，如果和kindle比的话，差很远。因为kindle 3的重量是246.6克，并且待机时间能达到30天。<br />
&nbsp; &nbsp;3. 不过后来想了想，好像市场上所有电子书都差不多是这样的优点，bambook和人家比，差距还是很大啊。上面这两点算是在缺点里面找优点了。<br />
<br />
然后来数落bambook的缺点们：<br />
<br />
&nbsp; &nbsp;1. 反应慢，很慢。虽然换了速度优先的模式，但还是慢。翻个页起码要一秒，快速翻页的时候等待的速度实在无法忍受。<br />
&nbsp; &nbsp;2. 没背光。虽然说所有E Ink屏幕都是这样，但盛大的bambook主打是绿色低碳。晚上我看个电子书要开着个灯，何来的低碳？而且看完以后还要下床关灯，大冬天的怎么办？<br />
&nbsp; &nbsp;3. 帐号集成不好。我在起点免费书架有29本书，VIP书架有4本书，云中书城有31本书，但在bambook上面显示的是云图有38本书，自有11本书。我完全不知道这个数字是怎么算出来的。而且买书我还要分开在起点和云中书城两边买，很麻烦。<br />
&nbsp; &nbsp;4. 能看的书太少，没有我喜欢看的书。如果bambook定位在网络文学我觉得没问题，如果定位要和传统出版行业竞争的话，旅途还很遥远。我是很希望bambook上面能购买传统的纸质书和杂志的，比如《南都周刊》。<br />
&nbsp; &nbsp;5. 书的价格太贵，而且在bambook上面购买和在web上面购买，价格还不一样。bambook上面购买，价格每千字贵2分钱。用户一不小心就当傻子了。<br />
&nbsp; &nbsp;6. 主打的听书功能，真不怎样。断断续续的，就跟听歌看片子时网络不好一样。听得我那个憋得慌啊，我都恨不得自己读给它听了。<br />
&nbsp; &nbsp;7. 菜单太乱，用户体验太差。翻页翻了十几页，想要回到第一页很麻烦。气急败坏的时候按下返回键，这下可好了，直接给你退出。其他各种不舒服的小细节不胜枚举。<br />
&nbsp; &nbsp;8. 键盘不好用。本来屏幕刷新就慢，还给来个小键盘。第一次连接家里wifi的时候，输密码花了我将近5分钟。比如输入一个z，我要按键4次，每次按键我要等1秒钟，等待屏幕刷新了才知道我是不是输对了。如果换全键盘就没这个问题了。我打赌以后通过bambook去写书评的人一定很少。“盛大的电子书怎么样呢？”这句话，我全部打完花了1分30秒，中间还因为屏幕显示慢打错了2个字。如果你想写长篇大论，省省吧。等你写完就发现你已经完全没脾气了。<br />
&nbsp; &nbsp;9. 没右手的翻页键。可能盛大的bambook设计者的确考虑到了一个情况：一般人都是用左手拿着书在看的。不错，但他们忽略了另外一件事：用左手拿书是因为要用右手翻书页，并且书比较沉。电子书由于不用真的去翻书页，而且比较轻，所以一般人都是用右手拿着，而且右手拿着还舒服。我周围所有人都是右手拿，然后傻乎乎的用左手去翻页，看来盛大在这点上的确做到了颠覆。2.0的电子书时代，右手拿书左手翻页的时代。当然，这也有可能是每个人习惯的区别吧。<br />
&nbsp;&nbsp;10. 不知所谓的云中书城（web版本）。打开书籍后看不到章节和预读内容也罢了，好多页面都没有头部。sdo的前端技术人员水平不咋的我是见识过的，比如注册盛大通行证的时候就不允许用.cc结尾的邮箱（其实是sdo的前端把允许注册的邮箱限制为.com/.net/.org/.cn这几个常见的结尾，对于.us/.name之类的就不闻不问了），但从来没见过套个页面还套成这样的。外面吹牛吹得一塌糊涂的云中书城，不过是一个临时拼凑没有任何产品设计和功能的烂东西而已。<br />
&nbsp;&nbsp;11. 网络功能。bambook如此简单的系统，有了网络功能也是没任何意义。还不如用USB传输来得实在。<br />
&nbsp;&nbsp;12. 价格。提到价格前我们不如先横向对比下现有电子书的价格吧。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; * Amazon Kindle 3，价格$189，折合人民币1283元，内置3G和WIFI，并且3G可以全球漫游（包括中国），一分钱不收（电子杂志订阅除外）。你可以拿着 Kindle 3去到全球任何一个有3G网络的地方，随意上网和下载电子书不花钱。246克重量比bambook轻，8.5毫米的厚度比bambook的10.3毫米要薄，4G内存比bambook大，全键盘，左右都有翻页键，30天待机，内置Web浏览器，原生支持中文。这些配置完全比bambook要好，价格也只要 bambook的内测价格998+285人民币。唯一缺乏的是中文电子书的购买，不过卓越如果引进后并且把卓越上面卖的所有纸质书都转换为电子版的话，bambook完全没有任何杀伤力。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; * 基于Android的山寨iPad：从5寸到13寸，系统从Android 1.5到2.2，价格从400块钱到1000多，各种款式任君选择。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; * 纽曼系列电子书：6寸，全彩TFT，触摸屏，TTS朗读，甚至HDMI输出全都有。只要680人民币。（无网络功能）<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; * 台电系列：6寸，E Ink，8.9毫米厚度，MP3，录音，SD卡扩展，自动横屏，TTS朗读，这一切只要999人民币（无网络功能）<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; * 汉王系列：好歹也是知名品牌，差不多配置的产品需要1800起（无网络功能）<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; * 爱国者：1400起（无网络功能）<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; * 盛大Bambook：定价3280，看来是在挑战大家的智商。就算是内测价格998，也只能算是一个比较合理的市场价。算上用户购买 bambook以后需要花费大量的钱在购买价格高昂的盛大电子书上，而且用户购买3G模块等设备还要花费额外的费用，这个价格并不合理。按照盛大的官方说法，以后硬件要亏本卖，然后从用户购买书籍上面赚回钱，那么我觉得bambook价格能降到500左右才能为用户所接受，才能抢占市场。否则就算最终的销售价格是998，也很难竞争过Kindle。首先因为Kindle配置高，能免费上网，而且比bambook贵不了几个钱（甚至比bambook便宜，如果bambook最终售价在2000以上的话），其次因为中国人还是喜欢拿免费的东西，Kindle或者bambook的电子书购买其实对国内用户没意义，用户买回来以后一样会上网下txt和pdf拷进去看。如果bambook价格能降到500，让用户入门，然后云中书城的价格再降降，因为有些电子书的价格比当当或者卓越买纸质书的价格还贵。降到用户能接受的范围，然后再把用户自有的电子书转换难度增加一下，那我相信用户肯定会去购买电子书。用户也怕麻烦，能让用户方便廉价的购买到电子书，让用户感觉花在转换文件格式上面所浪费的时间还不如去买一本电子书方便，这样才能有效的抑制盗版。其次 bambook可以大力发展电子杂志（传统杂志的电子化）。因为杂志这东西消费的人群多，价格不高，时效性强，难盗版。等有人费力气扫描传上网，杂志有可能已经过期了。有些热门的杂志在某些地区还买不到。所以做电子杂志的话或者真能闯出一条血路来。当然前提是把那破E Ink黑白屏幕丢了。<br />
<br />
最后总结来说，bambook算是一个基本功能相对完备的电子书产品。具备了基本的电子书“查看”功能，只能看书，甚至连看个英文书想要查个字典的功能都没有。更加别说上网或者做别的事情了。真要做这样的事情的话，买个山寨iPad都比bambook好，好歹不需要用一个黑白的屏幕来看PPT，价格还便宜很多。<br />
<br />
如果bambook想定位为一个整合互联网的电子书产品，那么就是大大的失败了。从网站到bambook本身，产品和流程设计都不如人意。专门设计的书评功能也因为键盘和输入法而变得鸡肋。有些功能可以通过日后的软件升级改善，但有些硬件上的设计缺陷则永远改变不了。我所了解到陈天桥对 bambook的项目很重视，但陈天桥和陈大年毕竟不是马化腾，缺乏对产品的把握能力，更加别说把握好从各处收购回来的项目了。一堆一堆收购回来的项目，不同的文化理念和管理方式，非要揉在一起做成一个大产品，最后只能是个四不像。<br />
<br />
