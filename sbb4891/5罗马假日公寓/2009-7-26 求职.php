																											<div id="postmessage_178773" class="t_msgfont">首先，我不是说教，主要是说说我看到的简历中的问题，目的是让大家能写出更好的简历，争取更多的面试机会。<br />
<br />
　　首先，我不是技术大牛，小coder一个，之所以负责招聘，是因为哥们创业，我是过来帮忙，对我比较信任。<br />
<br />
　　首先，我说的简历问题只是个例，别看完了都往自己身上览。<br />
<br />
　　这些天，收了无数简历，都看过了。职场专家说过，一份简历能让负责招聘的人看上30秒，就有点希望。我大部分简历连3秒都没看到，就删除了。<br />
<br />
　　写简历<br />
<br />
　　第一，你先要弄明白你自己想要做什么<br />
<br />
　　不同职位要有不同的简历才行，如果你的项目经历少的话，就重点写一下你认为很好的项目，并且写清你在里面都干了什么，有什么收获。<br />
<br />
　　这里，最好写清这个项目的钱途如何，要自己的理解!!有时候，虽然我们只是一个技术员，但是我们要是能给公司老总提出建设性的意见和建议的话，会对自己的发展很有好处。简历里面写清了这些，会让人感觉你很有思想，谁也不会想要一个一点思想没有的员工。<br />
<br />
　　第二，你要弄明白公司要的是什么? <br />
<br />
　　我们的招聘信息写的很清楚，要程序员，可是什么销售啊，什么会计啊，什么文秘啊，这些简历都收了不少。这种瞎猫碰死耗子的事情不是没有，但是，让人感觉你很不靠普，连最起码的招聘信息都没看就投了简历。就算我们同时也招聘销售，会计，文秘，我也不会从程序员的招聘邮箱里选择一个的。<br />
<br />
　　以上，这两点说的很明白。就是针对不同职位要有不同的简历才行。举个例子，我们招聘一名cs狙击手，大狙使得好的。你的简历里面写了会cs，还写了一堆关于打星际的东西。可是我们要的cs狙击手。大狙使得好! <br />
<br />
　　说白了，我知道做研发，重点是思想，思想通了，语言无非就是工具而已，会java的同时。net也玩得不错，但是我们招聘的是net，你要重点写这个，java一带而过就行了。<br />
<br />
　　第三，你要了解这个职位，这个行业，你的工作经验的待遇<br />
<br />
　　期望待遇这东西不要乱写，写的高了，和你的项目经验不符的时候。人家会觉得你华而不实。直接pass了，写的低了人家会觉得你没啥实际水平。项目只是参与而已。<br />
<br />
　　第四，简历不要用附件<br />
<br />
　　我的邮箱没有屏蔽附件邮件的功能，但是有的邮箱有这个功能，可能你的附件邮件人家都没收到。同时，你也为我们想想。我每天一堆代码要写，还要看简历，经常半夜1点了开始看一天的上百封简历，说真的，你一个邮件，里面就写了个名字，然后带个附件，我真懒得看。<br />
<br />
　　第五，就是告诫应届的朋友<br />
<br />
　　谁都有第一参加工作，知道你们也不容易，我尽量找应届朋友来。但是您的简历我实在是没法往老总那里推荐啊!! <br />
<br />
　　给你们看看我从多份简历中总结的一段(注意，是多份简历总结的，不是摘抄!别往自己头上装): <br />
<br />
　　我是****大学计算机专业的07届本科毕业生。大学四年，我具备了扎实的专业基础知识，曾荣获“学习进步奖学金”。在计算机方面，我已通过了国家计算机二级c语言，能熟练操作WINDOWS系统，能熟练运用Microsoft Word等各类办公软件。我的英语已顺利通过了国家四级考试，具有较好的实践运用能力。<br />
<br />
　　看到了其中的问题了吗?作为一个计算机专业学生，首先计算机二级是小儿科，该专业本科毕业就是3级水平。熟练使用windows……在国内，windows如此普及，有什么可写的?word办公软件……你是要应聘文秘吗?这简历开头完全像是一个初中毕业的人的简历。我还有什么信心在看下去呢? <br />
<br />
　　最后，就是一定要切合实际<br />
<br />
　　简历写的再好，无非就是能争取面试机会。实际水平如果不行，也会在面试的时候被刷掉，行家一出手，就知有没有，把你叫来面试，和你聊两句，就知道你是真的会还是假的会了。这样就是白白浪费路费。说真的，想要好工作，就必须提升自己的能力才行。大学的时候没好好学习，什么都不会，那出来就踏踏实实的找个低端一点的工作，在补回大学每天包夜打游戏欠下的债吧!苦果是自己做的，自己吃吧。<br />
<br />
　　还有，不要报怨大学不行，大学不是小学，大学教会的不是知识!!知识是要通过一生来不断的学习的能力。大学就是让你开始学习自己喜欢的东西的一个开头而已。大学什么都教不了你的!!　　<br />
