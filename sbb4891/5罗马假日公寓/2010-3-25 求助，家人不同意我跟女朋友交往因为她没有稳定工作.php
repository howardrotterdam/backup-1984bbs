楼主，真不觉得你父亲是无条件地爱你。这恐怕是他们让你这样认为的吧，这样你就会时时对他们内疚了<br />
&nbsp; &nbsp;其实家是最伤害人的地方，真正无条件爱孩子的，是会包容孩子的选择，爱孩子所爱，所谓爱屋及乌。 但是在你所 ... </blockquote></div>这话不假，但是问题也要逆向思维，很多父母以爱伤害孩子确实令人痛心，但是将之和独裁者类比还是不合适的，毕竟那些真正能对子女痛下杀手的父母还是极少数。<br />
个人感觉有时候父母就像孩子，每次孝敬他们东西，他们总是说，干嘛那么浪费，好像生气的样子，但在外人面前却很高兴得提起：“孩子又给我啥东西了”，因此我觉得要解决好这问题，就要在心态上把父母当孩子，对方不对的观点不要迁就，要说服，别在心理上感到父亲是一种压力，要有一种把握局面的自信，能找到这种感觉你处理这事就很轻松了，<br />
我同意楼上一些人的观点，要想破解这种僵局，关键是要让父母认识到你已经能独当一面，这是根本的。在保持自己独立决定的同时，让老爸知道自己其实是在乎他的（老妈是个好途径），最终他还是会接受的。至于生米熟饭的问题，如果能争取老妈的认可，不失为一个可行的途径，那样个人感觉好得多，把握也更大。即使暂时没有老妈的支持，如果真遇到好姑娘也千万不可轻言放弃，找个好人不容易！<br />
