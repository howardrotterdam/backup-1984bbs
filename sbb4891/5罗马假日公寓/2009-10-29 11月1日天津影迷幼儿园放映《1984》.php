新堡是一个比较偏远的山区村庄。 村脚下有一座小煤矿，由于过度的开采，导致山体变形，房屋裂缝。 在政府“新农村建设”的倡导之下，普洞村委规划将新堡人全部迁入即将建好的“小洋楼”之中…… <br />
