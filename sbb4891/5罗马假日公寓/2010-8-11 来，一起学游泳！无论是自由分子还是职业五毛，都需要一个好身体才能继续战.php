3、手臂与水平面垂直时，经手领先，加速推水，手臂转为向外、向 上和身后划水直到大腿侧，提肘出水。&#173;<br />
4、出水后，手臂自然、放松地经空中向前移臂，保持高肘姿势。然后手在肩前领先入水，开始下一个动作。<br />
