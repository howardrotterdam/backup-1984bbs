2、内划。掌心由外转向内，手带动小 臂加速内划，手由下向上并在胸前并拢(手高肘低、肘在肩下)，前伸。&#173;<br />
3、前伸。双手向前伸(肘关节伸直)。 要提醒大家注意的是：外划是放松的，内划 是用力的、加速完成的、前伸是积极的。<br />
