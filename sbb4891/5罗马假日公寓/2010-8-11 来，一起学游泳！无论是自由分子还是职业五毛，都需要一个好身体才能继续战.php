&nbsp; &nbsp;自由泳的完整配合有多种形式。一般常见的是每划水2次，打水6次，呼吸1次。<br />
<br />
<img src="http://upfile.kdnet.net/UploadFile/2010-8/2010810225148540.gif" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
