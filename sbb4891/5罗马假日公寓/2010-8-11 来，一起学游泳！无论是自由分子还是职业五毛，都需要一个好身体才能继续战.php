																											<div id="postmessage_778323" class="t_msgfont">学了30多年的游泳，去年才学会。<br />
<br />
从小在河边长大，每年暑假都跟大人一起去水库和河里洗澡，也经常逃课去池塘和湖里玩，会些狗刨，但一直不会游泳。<br />
<br />
为了增加浮力，从小就爱动脑筋的我发明了无数土办法，现在能想得起来的有<br />
<br />
1.手上拿2个大的可乐瓶子<br />
<br />
2.抱着脸盆<br />
<br />
3.抱一个汽油桶<br />
<br />
4.套汽车轮胎<br />
<br />
5.游泳圈――我和弟弟是村里唯一拥有真正游泳圈的人，所以我30多年后才学会游泳<br />
 <br />
以上方案其实都只能在水中漂浮，无法游起来，于是<br />
<br />
6.我甚至自己动手做过救生衣，就是找些泡沫，和2件不用的背心，把泡沫缝在背心里，再用带子绑在身上<br />
<br />
……诸如此类<br />
<br />
工作后，夏天偶尔去游泳池，打算学会，但一直不得法，总是狗刨个20来米，或者费劲巴拉的游到泳池对面，然后精疲力尽。连续几年，我们一群老哥们都在京郊找地野泳，我只能穿救生衣下水同乐。非常不爽，痛下决心学会游泳。<br />
 <br />
<br />
××××××<br />
<br />
终于在去年找到了突破，现在可以比较轻松的游1000多米，也敢在水库、湖里游了――就是心理还有待克服――不敢游远。<br />
<br />
 <br />
下面说些我的感受：<br />
<br />
 <br />
健康最重要，平时保持运动的最好办法就是游泳<br />
<br />
<br />
1.对从未、很少学过游泳的人来说，可以从任何泳姿入手，这取决你的教练或者同伴或者自己的悟性，先培养水感<br />
<br />
2.对已经很有挫折感的成年人来说，克服恐惧心理才是第一步要做的，应该推荐蛙泳开始学，我自己就是先学蛙泳的，很快找到感觉后，自学了仰泳，正在学自由泳，蝶泳放最后学――也可能学不会。<br />
<br />
3.所有成年人都知道人的比重小于水，只要放松就可以漂浮在水面的。而且泳池也很浅，随时可以站立休息。学不会的人，全部是因为紧张造成的。所以，成年人学游泳，第一步就是克服紧张心理，要放松。<br />
<br />
4.“放松”估计所有教练和同伴都是这样强调的，如何“放松”？才是需要经验交流的<br />
<br />
以学蛙泳为例，全过程分解为：憋气漂浮――憋气分解动作（脚）――憋气合成动作（手脚）――学换气――熟练<br />
<br />
4.1先学会憋气漂浮，体会水的浮力，感觉身体在水面漂起来。<br />
<br />
4.1.1这步最简单，先深吸一口气，然后全身放松，面部朝下，平躺在水中。这个阶段，手脚都不要动，自然放松。直到气竭，站立休息，然后反复几次。（在浅水区练习。不要去儿童区或者戏水区，那太浅了，没法练）<br />
<br />
4.1.2为什么面部朝下？这是为后面学蛙泳做准备。如果是要先学仰泳，那就面部朝上。根据我的经验，面部朝上时，初学者心理恐惧会非常大，反而没法放松，所以就从面部朝下开始学憋气漂浮。<br />
4.1.3憋气漂浮练到能体会到只要身体放松，就能漂起来的感觉就可以结束了。憋气时间不低于半分钟，当然憋气时间是越长越好，时间越长越容易体会到水的浮力和身体漂起来的感觉。这就是所谓“水感”的开始。<br />
<br />
4.1.4憋气漂估计练个10次就ok了，30分钟左右就可以结束。<br />
<br />
4.2接着练憋气分解动作，先练脚的动作。这部分练习可以先用手扶着泳池壁来练习。动作要领挺简单，就是“蹬-收-蹬-收”，很多视频和教程都有详细的，就不多说了。一开始不用讲究动作多标准多规范，先体会如何发力。<br />
<br />
4.2.1站立在水中，把双手举起，交叉握紧，把双臂紧贴耳朵，最好是夹紧头部，双臂贴在耳朵后（这个动作很重要！！），深吸一口气，然后入水<br />
<br />
4.2.2在水中先让身体保持漂浮状态并保持这个手臂姿势（夹紧头部，双臂贴在耳朵后），然后做脚的动作“蹬-收-蹬-收”，能做几个算几个，手不要动，脚的动作要做得慢一点，尤其是二组“蹬-收”动作之间，要有意识的让身体滑行一下，体会到每一次“蹬-收”动作后身体在前进的感觉――这就是“水感”。<br />
<br />
4.2.3气竭后站立休息。反复联系。<br />
<br />
4.2.4一开始练习这组动作会慌乱感，保持镇定后，就能持续动作了。刚开始的常犯的毛病是拼命蹬腿，这样很快就气竭，而且更容易慌张。要有意识的提醒自己，动作做的慢一点，清晰一些。在二组“蹬-收”动作之间要有意识的让身体滑行一下。这样憋一口气就能游挺远的。<br />
<br />
4.2.5整个练习中，手的姿势不要变，入水后，更要有意识的用手臂夹紧耳朵<br />
<br />
4.2.6一开始一口气大概可以做2-3组脚动作，大约游3-4米。反复练习后，能做6-7组动作，或者游10来米了。能熟练憋游半程左右（我们以25米小泳池作为练习场地）那就可以练憋气手脚动作了。<br />
<br />
<br />
4.3因为要练习手动作，所以入水前双手合十就可以，还是一样双臂举起来，贴在耳后。深吸一口气，入水。<br />
<br />
4.3.1蛙泳动作标准就不细说了，很多视频和教程。但无论哪个教程都会说，蛙泳的手脚动作要分开做，初学者要做到这点不容易。但没有任何捷径，只能是在水中有意识的提醒自己。这时“放松”“水感”的作用就特别明显。心里越放松，水感越好，在水中就越清醒，就能体会动作完成的过程，就能有意识。不然就是两眼一抹黑，大脑一片空白的在水里扑腾，气竭而立。<br />
<br />
4.3.2因为这个阶段不用换气，所以可以很专心的去体会脚、手动作，体会身体在水中的前进。需要提醒的还是前面已经反复提醒的，动作要慢，要清晰，手脚动作分开，2组动作之间要暂停一下，体会身体漂浮前进。<br />
<br />
4.3.3手脚动作同时练习是为练“换气”做准备的。只要会换气了，才算会游泳了，才能再水中不停的游下去。<br />
<br />
4.3.4这步练习特别需要体会的就是手在“压水”这个动作时头部有上扬出水的感觉。这一定要有意识去感受。很多朋友一直学不会换气，首先就是心里特别害怕头不能伸出水面，无法呼吸。要不就是头一直在水面的游（高手做到这点很容易，而初学者这样做的话，肯定游不了20米），要不就是手脚动作还不到位的情况下使劲抬头（这种情况下特别容易呛水）。<br />
<br />
4.3.5蛙泳里“抬头”这个动作是手压水动作后自然而然引发的，无需特意抬头。反复憋气练习，能感觉到手压水后头自然抬起后，就可以尝试练换气了。<br />
<br />
4.4换气。其实能体会到4.3.5的动作后，换气就是自然而然的事了。<br />
<br />
4.4.1需要再次强调的是“动作要做得慢，要清晰”，动作清晰且慢的话，头抬出水面的时间就越长，我见过最长的能有3秒左右，其实1秒就足够让你换气了。越长就越从容不迫，越安全。我们不是比赛，无需赶速度，拼命游只会让你很快力竭。<br />
4.4.2初学时，可以抬头一次完成呼气和吸气2个动作，这样做比较容易呛水，而且显得很紧张。逐渐熟练后，就可以找到自己的节奏，分开呼气和吸气的动作。这一切都需要在水中清晰的意识。<br />
<br />
到此，就算学会游泳了。反复练习后，水感自然而然就来了，就越容易方式，再学其它泳姿也容易得多。<br />
<br />
×××××××××<br />
有朋友说，一开始要从踩水学起。能踩水的地方最少需要1.8米以上的池子，要不就是水库、湖泊。这些地方初学者根本就不敢去，也不能去，如何学“踩水”？<br />
<br />
踏踏实实从蛙泳学起，从憋气学起，反而快。<br />
<br />
以上教程，全是我的多年学游泳而不得的亲身体会，相信会给和我一样在游泳上有非常多挫折感的朋友一些帮助。</div>

							
							
							
							
