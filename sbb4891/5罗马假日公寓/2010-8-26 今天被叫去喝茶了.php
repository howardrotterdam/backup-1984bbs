													<h2>今天被叫去喝茶了</h2>
						
						
																											<div id="postmessage_798739" class="t_msgfont">今天上午9点多，接到导员来的电话，叫我立刻去一趟行政楼。我猜到是国宝来找，因为开学前听朋友说许多给冯正虎捐了89.64元的人都被找过了，她还调侃说你会不会是咱学校第一个被喝茶的呢。我一想，哪能啊，至少得先算上刘晓波啊。<br />
不过去的路上还是不确定的，或许只是一般的事情呢，比如逃了课啥的。给朋友打电话，谈及此事，她也劝我不必担心，还给我说网上看到的经验，比如哭之类的。虽然我肯定不会采用，但和她一路聊天也缓解了紧张的心情。<br />
到了导员告诉我的那个房间，里面有两个四十岁左右的男子在等着。我坐在靠门口的椅子上，一个男子拿个本子和一张小表格坐在我对面，另一个稍胖的坐在靠窗的椅子上。看来是我面前的这位负责说话了。<br />
他一上来就开门见山，说他们是公安局国保支队的，找我来了解一下情况，让我不要有什么心理负担。然后就直接问我6月27号是不是给上海一个叫冯正虎的人汇了89块6毛4，并让我说说这个事。我承认。然后他问我了解冯正虎的什么情况。我就说知道冯在日本机场的事和他以前是上海什么大学的教授，别的一概不知。他又问是通过什么渠道知道的，我就说校内网，还解释了半天什么是校内网，那个胖国保倒是知道，也跟着我解释，最后他在本子上记下了千橡集团的校内网，大概是为了统计消息来源。感觉很对不起千橡啊，毕竟我得知的渠道是小组和推特，但当时我只想到这一只替罪羊了。本来还想去参加千橡的校园招聘会，但愿这事不会传到那里。<br />
说完汇款的事又说汇款数额的事，他问我为啥汇89块6毛4，结果话题自然就引到六四事件了。之后让我说对这事的看法。我不想浪费时间，就说两方面都有自己的说法，不过目前都不可信，但这个日子应该记住。他们应该是很介意这个日子被记住的。但估计是看我没什么别的观点，也就不再说这个事。只说这个将来由历史去评价。<br />
后来他们又跟我说冯正虎是六四的重要人物啊、与美国一些敌对势力串通啊、暗地里搞民主活动啊之类的事，我全都说不知道。<br />
他们还问了家庭地址，父母姓名和工作，我的电话，还有我的院系和导员的名字。<br />
最后他说我这个事说大也大，说不大也不大，他们作为国保，说白了就是为共产党服务的，维护共产党政权的。大学生应该有正确的人生观价值观。他还拍着之前那个小表格（我猜测是发给各地国保的名单）说这是公安部通报下来的，因为我是在校学生，所以通过保卫处来找，也不用做笔录了，只是了解一下情况。要是社会人员就直接叫到公安局里去了。<br />
对于他的大部分说教我都是以“嗯”或“是”来作答的，为了节省时间。因为下午实验课要交程序，还没有弄完，不想在这里多耗着。感觉两位国保一主一辅，一个扮黑脸一个扮白脸，负责谈话的那位一直都是很严厉的口气，而那位胖国保则经常在旁和蔼地做解释。第一次见国保，难免紧张。进去之前我用手机录了音，一共18分钟，后来再听一遍，对我的表现不是特别满意，而且感觉声音不像自己的，是一种二百五的声音。<br />
不晓得这算不算是被认证了，也不知道我是不是被登记在册了。现在想起当时情景，很不清晰，跟做梦一样。<br />
录音不打算放出来，感觉很糗，而且涉及私人信息。等我学会往音频里加【--哔--】的声音之后，再考虑是不是放出来供未喝茶人士做参考。<br />
