																											<div id="postmessage_458809" class="t_msgfont">爱家乡的传统包括语言是一种内心亲情的表现，是非常值得赞赏的。但对任何语言都不应该有歧视，楼主之所以对普通话有不满，佳因共党政权从来不尊重民意，不论推行什么都以用强权方式，引起人们内心的反感。楼主的家乡确实是“珠三角”之中的好地方，希望你们能把家乡的传统文化包括语言发扬光大。<br />
