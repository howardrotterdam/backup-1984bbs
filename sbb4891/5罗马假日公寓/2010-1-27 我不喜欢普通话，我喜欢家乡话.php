&nbsp; &nbsp;&nbsp; &nbsp; 本人曾有一作，为上海人因普通话的问题被外地人责骂而鸣不平，请楼主评点一下。有空到你家乡吃“石歧乳鸽”。&nbsp;&nbsp;<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;《上海人没有错,错的是官霸恶霸文化思维令普通话蒙羞》<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 上海因两次“普通话事件”闹得中国大陆网民热闹非常，上一次是当地一份报纸有位记者竟然在报上发表了一篇关于“讲上海话是代表没文化”为内容的文章，引起上海市民强烈的反感，最后据说这位记者被炒了。于是有人就此指责报纸的高层和上海市民，认为不应该这样对待这位记者。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 上海市民的态度没有错，报纸的高层也没有做错。试想一下，如果一个人连自己家乡的文化包括语言都不热爱，还能企望他（她）爱自己的国家和自己的民族吗？另外，这份报纸销售对象百分之百都是上海市民，他们就是这份报纸的“米饭班主”，这位记者如此羞辱上海市民，分明是在“倒米”，作为上司，难道还要给这样的“倒米寿星”（粤方言，指自招损或损害自己一方利益的人）加薪升职？<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 这一次，则是一个带有极端化情绪的听众发了一条“求你们不要说上海话了，我讨厌你们上海人”的短讯给电台主持人，而主持人则给了一个合情合理或叫人之常情的答复：你讨厌一样东西一个地方或一个人，离开就是最好的选择！作为听众，你明知这是上海的地方电台，理所当然就应该说上海话，你可以选择不听，去收听中央台或讲普通话的电台，而你却偏要来个“宾主颠倒”，甚至还要讨厌别人。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 网上有“拍砖”者批评这位主持人“利用公器发私怨”，这种说甚为可笑：作为主持人在选择一些问题作答复时，当然是选择比较尖锐或有代表性的问题作答，主持人有与这位听众与其说有私怨不如说他代表了一种正直之声。这位听众所代表的是目前在中国大陆部分人的不良心态：他们既向往大都市的经济和文化的繁荣，想立足其中创一番业或得以安居其中，但又不以谦和的态度，了解和学习当地的人文习俗，历史文化以及生活习惯等，遇到自己听不懂或者不明白的人和事，就讨厌就反对，祈望来个“宾主颠倒”关系。这些人当中，相信有些还读过点书，可能还是大学毕业生什么的。之所以有这种心态和态度，皆因中国大陆一直所施行的教育从来就是唯官唯上，此烙印复刻到个人身上，也有一种追求别人服从我，别人一定要说合我心意的话，或能给到我所希望的东西， 否则，就视之如仇敌。这就是中山大学历史系教授袁伟时所说：唱狼奶长大的。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;普通话作为一种语言，与其他任何一种地方语言一样，都是非常美丽的，官方推广，也没有错，因为它具有比较广泛的适应性。但在推广的态度和作法上，却不能以一种强迫的方式进行，例如在公共场合大条标语：同志，请讲普通话；在中小学甚至在幼儿园要求学生们除课堂用语外，连私下交谈也要用普通话，如果不慎讲了句地方话，就可以被其他同学作为罪状告到老师那里，普通话就成了“普通罪”。更有甚者，连听到在公共汽车上除了用普通话报站，再用一句地方话报站都认为不行，极为浅薄又可笑地认为：要成为“国际大都市”就不应该有地方文化和地方语言在公共场合出现，大有不灭地方方言誓不罢休之态。典型的官霸恶霸文化心态，到底谁更令人讨厌便不言而明。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;之所以说他们浅薄和可笑，因为首先他们不明白：越有地方性就越有世界性的道理。另外，任何国家和民族传统文化的传承，有大小两种不同的传承方式，一个是“大传统”：即官方以国家名义推行，但这很容易断裂的，例如发生战争甚至亡国。如东南亚和非洲一些国家，与欧洲相距十万八千里，却以英语或法语作为官方主要用语，无论书写和日常对话。与大传统相对应的就是“小传统”，即地方的以致是乡村的，有比较清淅的界限，也非常稳固，对民族内在核心维系很有作用。一些文化圣人例如孔孟先师，李白杜甫等，还有一些被公众认为是忠义之士、正气化身，例如关云长岳飞以及包公等，都有不同的地方以至于乡村的文化教育，戏曲表演以及说书故事等方式加以传承。经典优秀的文字文化读本例如“四书”“五经”，唐诗宋词以及《三字经》等等。当人们用京腔、湘音、粤语、上海话以及东北话等等去吟唱，朗读时，是何等的动人心声，回味无穷。这时难道你不觉得中华文化的“博大精深”并非是浪得虚荣？！如果中华大地每一个角落都只剩下普通话，难道你不觉得这个民族已经走到尽头了吗？！如果人类只剩下一种语言，这与飞禽走兽靠声音的高低，频率和时间的长短来表达所需、情感甚至是雌雄有多大区别？！<br />
