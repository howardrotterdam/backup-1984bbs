<br />
中国和哪些国家签署了引渡条约<br />
来源：环球时报<br />
时间：2010-01-26<br />
<br />
<br />
随着各国间政治、经济、文化及民间交往的日益深化，涉及两国或数国的跨国性犯罪也日趋严重，成为国际社会关注的重大问题。有的犯罪分子实施犯罪后千方百计逃往国外，试图逃避法律的制裁。为将逃犯缉拿归案，加强国家间的引渡合作已成为打击跨国犯罪、维护对外交往法律秩序的必要手段。<br />
<br />
引渡是指一国应外国的请求，把在其境内被外国指控为犯罪或判刑的外国人，移交给请求国审理或处罚的一种国际司法协助行为。引渡的主体只能是国家，有以下3类国家：1、罪犯本人所属国；2、犯罪行为发生地国；3、受害国。<br />
<br />
引渡以条约为依据。因为国际法上没有赋予国家以引渡的义务，在没有引渡条约的情况下，相应国家可以拒绝他国的引渡请求。有了引渡条约就不一样了。引渡条约一般会约定两个或多个国家之间关于互相引渡罪犯的相关条件和其他事项，引渡就成为了国际义务，没有正当理由不能随便拒绝引渡。<br />
<br />
政治犯不引渡已成为各国公认的国际法原则。最早在1793年法国宪法中就规定了政治犯不引渡原则，但各国对政治犯的界定并不一致，由于属地管辖权的原则，被请求国可以决定是否引渡。<br />
<br />
1993年，中国与泰国缔结引渡条约，这是中国签署的第一个引渡条约。截至目前，中国已经与32个国家签署引渡条约，分别是：泰国、阿拉伯联合酋长国、菲律宾、蒙古国、印度尼西亚、韩国、巴基斯坦、老挝、阿塞拜疆、哈萨克斯坦、吉尔吉斯斯坦、柬埔寨、乌兹别克斯坦、立陶宛、俄罗斯联邦、白俄罗斯、保加利亚、罗马尼亚、阿尔及利亚、法国、西班牙、乌克兰、葡萄牙、莱索托、南非、安哥拉、突尼斯、纳米比亚、秘鲁、墨西哥、巴西、澳大利亚。</div>

							
							
							
							
