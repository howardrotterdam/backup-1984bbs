													<h2>支那</h2>
						
						
																											<div id="postmessage_800672" class="t_msgfont">支那（梵文：cina），亦作至那、脂那等，乃是梵文佛经中的一个词汇，意指中国，支那原为隋唐时佛教僧侣的一种汉字音译，一般出现在汉译佛教经典中。<br />
<br />
这一汉字词汇于9世纪初通过佛教交流传入日本。18世纪初，日本学者在学习兰学（荷兰学）时，将“支那”一词与西方词汇“China”对应起来，作为其在日语中的梵音译词。（China英音译词则为チャイナ）<br />
<br />
明治维新后，“脱亚入欧”的思潮在日本日益兴盛，“支那”一词开始在日语中大面积地替换原有对中国的各种称呼。在日语中，该词的汉字写法与中文一致：支那，假名为“シナ”，罗马字拼写法一般有两种：“shina”（平文式罗马字）或“sina”（训令式罗马字）。<br />
<br />
甲午战争令日本开始跻身东亚地区强权的行列，而中国的国力则进一步衰弱。在1912年宣统帝退位，民国成立，中国有了一个新的正式称呼“中华民国”，但“中华民国”这个国号并没有立即得到世界各国的承认。清王朝倒台后，中国发生了内乱，各省独立，南方的革命党在金陵成立了一个叫中华民国的临时政府，但那时中华民国的范围只限于南方数省，北方的大片土地并不属于中华民国。此时清王朝倒台，已不能称中国为“清国”了；而称中华民国也不合适，一是因为那时中华民国还不能代表中国全国（南北和谈后北方才承认中华民国的国号）；二是因为日本政府一段时间内并没有正式承认中华民国。在此情况下，日本政府放弃“清国”的称呼转而用民间惯用的“支那”一词称呼中国。1913年7月日本政府明文规定：今后不论中国的国号如何变化，日本均以 “支那”称呼中国。1930年代时，日本已决定在外交正式场合弃用，但是“支那”一词依然为日本上下广泛使用。直至第二次世界大战日本战败之后，“支那”一词才从日语的正式场合中全面退出。<br />
<br />
<font color="red"><strong>如今，中日等国的民族主义者对使用该词是否在带有敌意上存在争议，一般的共识是：以个人身份屡次地或者在正式场合使用“支那”称呼中国，或称呼中国人为“支那人”，是一种不礼貌、不尊重中国且带有藐视意味的行为。</strong></font><br />
