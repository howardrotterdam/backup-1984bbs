　　港台剧中，黑社会人物往往称女朋友为“马子”，如“敢动我马子，找死！”“她是孙老大的马子。”将马子理解成女朋友似乎太高雅了点，“马子”往往指这样的女性：她在人身上依附于她的男友，没有自身的尊严与人格，大多数实际上充当的只是男子的情人，男友跟她在一起，与其说是因为爱情，毋宁说是因为性与面子。无独有偶，在东北，俗语里也有“马子”的说法，指“暗娼或不正经的女子”（古敬恒《人体词与人的秘密》），据说其语源在“可任人骑”。“骑”不但是性交的粗俗比喻，更是指男性对女性的征服、玩弄与欺侮。自从港台剧在大陆盛行以来，“马子”一词也泛滥开来。 <br />
　　因此，泡马子一词应该是由于马子一词流行后，用它来代替“泡妞”一词中的“妞”字而形成的。<br />
　　“马子”的原义为“尿壶”，在民间尿壶原称“虎子”，唐代李渊建国之后，因为李渊父亲名为李虎，遂避讳叫“虎子”，改为“马子”，后引申义为供男人临时发泄的物品。在近代，由香港黑社会电影普及给大众，贬义含义有所弱化。</div>

							
							
							
							
