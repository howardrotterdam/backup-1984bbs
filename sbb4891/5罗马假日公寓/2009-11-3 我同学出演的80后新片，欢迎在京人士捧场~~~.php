<br />
剧情简介<br />
<br />
30岁左右的一对小夫妻，在一片“涨”声中，下了买房子的决心。起初相中一套两居，但后来由于虚荣心和物欲的不断膨胀，最后不惜血本，巨额贷款买了别墅。因此背上了沉重的经济负担，彻底沦为房奴，各种琐事接踵而至。夫妻感情恶化，领导关系僵化，让这个原本美满的家庭濒临破碎。最后两人认清现状，卖掉了别墅，买下了最初的两居，生活重新美好起来。<br />
<br />
导演阐述<br />
<br />
在二次创作阶段，我以自己的理解对于原剧本适当进行了一些改动，主要是以我们这一代人的角度来看待买房热这一问题。对于剧本最大的改动就是结尾，因为我在想，我们为什么要如此在意别人的看法，为什么要被那些莫须有的琐事烦扰，为什么要被房子牵着走。一栋豪华别墅，它本应是美好生活的一个象征，到头来我们却把它当做是瘟疾一般避之不及，转手卖掉，退而求其次去接受那所谓的幸福生活。我认为这样的话，人物就显得太被动，太消极了，体现不出我们这一代人，其实也是所有中国人的拼搏向上和努力进取。所以，我的主人公不会违背他们最初的追求，他们就是要住别墅，就是要过最理想，最美好的生活。旁人尽管取笑，尽管多言，他们绝对不会放弃。这正如好莱坞崇尚“美国梦”，概括起来很简单，就是只要肯努力，必定会成功。其实这是一个放之四海皆为准的规律，是每一个民族都会积极宣扬的精神，只是中国电影目前还没有好莱坞强调得如此纯粹罢了，我们也应该有自己的“中国梦”。由此，也浮现出了我这一部电影最主要去试图表达的内容――梦想。<br />
<br />
我一直强调，我们中国所有的电影工作者要团结，而我们这一代的人更应该团结。这不光决定了我这一部戏的成败，它还决定着中国电影的未来。我们的剧组可以如此团结，我欣喜若狂。也正因如此，剧组变得并不只是一个机械化的，分工合作的形聚神散的劳动团队。它真正成为了一个整体，大家齐心协力，使着同一股劲。我们每天都会坐在一起认真讨论，反复推敲，我会广泛争取大家的意见，这也确实使得片子的水平不断提高。大家都处在一个极其浓厚的创作氛围之内，而非仅仅是在干活儿。现在呈现出来的影片，有很多很有意思的细节或设计都是在这样的氛围中聊出来的，仅凭我一己之力，绝不可能达到这样的效果。最让我感动的是，在即将杀青的倒数第二天，我由于急性肾结石的发作而不得不到医院去进行治疗，然而剧组并没有因此而懈怠，在副导演的带领下有条不紊地继续进行着拍摄计划，按时按量地完成了规定任务，没有因为我个人的原因而耽误了本来就十分紧张的拍摄进度。我想说的是，这就是一种信任，建立在我们彼此之间的信任，我认为它至关重要。<br />
<br />
在镜头拍摄方面，我也有自己的一些基本倾向。我称其为“楞”，大部分的喜感都来源于这种干脆的跳跃和错位拼接等等。当然，最后呈现出来的片子也许并没有把原先的设想实现得特别理想，只是按照常规手段和规律来剪辑表现。但最起码我们还是超着那个方向去努力的，有这样一个创新的意向，以及对于有所突破的期许。这也反映了我在镜头掌控，合理规划等等方面的能力还有所不足，还需要更多的机会来加强训练，进一步提高。这一次我们也启用了真正的原创配乐和歌曲，在我自己完善和规范视听的学习道路上，又迈出了坚实的一步。<br />
