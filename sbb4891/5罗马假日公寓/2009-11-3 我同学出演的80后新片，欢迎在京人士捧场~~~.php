徐超简介<br />
<br />
现已改名徐睦原，2000年凭借《甜缘》获得全国新概念作文大赛二等奖，从此踏上了文艺道路，又相继2001年凭借《流浪，瞎胡闹的青春》获得第三届新概念作文大赛一等奖，2002年凭借《我们在一起》获得第四届新概念作文大赛一等奖，成为新概念作文大赛有史以来第一位蝉联卫冕这项全国大奖的少年作家，而后，徐超致力于80一代文化的发展，先后编辑出版了《有多少青春可以重来》《爱是一个洞》等十几套畅销书。并于2004年出版了第一部个人长篇小说《一个人的北京》。<br />
<br />
2002年，徐超以优异的成绩考入北京电影学院，先后拍摄了电影《那些青春》《那时候》《歆歆》等作品。2006年毕业于北京电影学院，年仅22岁的徐超拍摄了个人第一部长篇电影《我把温柔献给你》，2008年拍摄了由小说《一个人的北京》改编的胶片电影《像电影一样生活》，2008年冬拍摄电影《房子房子我爱你》任导演。<br />
