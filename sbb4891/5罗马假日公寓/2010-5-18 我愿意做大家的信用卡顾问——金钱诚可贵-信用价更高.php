<br />
准确的说，如果你连最低还款额都没还够，你已经在央行个人信用报告中有了逾期记录。逾期记录是按月上报的，如果你未还清最低还款额的月份已有10个月，那么你拥有10次逾期记录，而不是1次。<br />
<br />
首先，这种情况千万不要销卡！<br />
一张信用卡带来的坏的记录，只能够通过继续长期良好使用同一张信用卡，才能慢慢的扭转为好的记录。<br />
只要保留这两张卡，你就还有机会恢复良好的信用记录。信用记录是若干月份滚动一次的，就像欧洲联赛的欧冠积分一样……<br />
<br />
你需要的做是：<br />
1.尽快想办法把欠款全都还清。（目的：停止继续制造逾期记录）<br />
2.还清后继续使用这两张卡，每月都要用至少一次，只买一瓶矿泉水也可以。（目的：获得翻盘的机会）<br />
3.每月在最后还款日之前按时全额还清。（目的：把握翻盘的机会）<br />
4.短期内不要去央行查询个人信用记录。（原因：每次查询个人信用记录，都会导致信用等级下降。虽然不合理，但规则就是这么定的）<br />
<br />
希望可以帮到你。</div>

							
							
							
							
