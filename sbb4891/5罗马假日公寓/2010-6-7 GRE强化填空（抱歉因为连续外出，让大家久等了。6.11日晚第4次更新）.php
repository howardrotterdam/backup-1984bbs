&nbsp; &nbsp; (D)&nbsp;&nbsp;reported on&nbsp; &nbsp; (E)&nbsp;&nbsp;agreed on<br />
这是一道很简单的转折关系的题目。前面说尽管两个人属于不同的政党，但是两个人还是对一个issue怎么样。明显是一样嘛，取个反。于是，这道题选E。<br />
