(18) Current data suggest that, although ------ states between fear and aggression exist, fear and aggression are as distinct physiologically as they are psychologically.<br />
(A)&nbsp;&nbsp;simultaneous&nbsp; &nbsp; (B)&nbsp;&nbsp;serious&nbsp; &nbsp; (C)&nbsp;&nbsp;exceptional<br />
(D)&nbsp;&nbsp;partial&nbsp; &nbsp; (E)&nbsp;&nbsp;transitional<br />
这道题关键是要抓住although和distinct。说虽然恐惧和攻击之间存在一个什么样的状态，但是两者还是截然不同的。很显然的，要选一个相同的状态。但是这道题真正的难点是在选项。选项里并没有一个很直接的表示“相同”的词。所以很多同学就选了A：同时发生的…毕竟有个“同”字，对吧。但是为什么不能选A呢，举一个很简单的例子。在同一个时候，我在上海某个教学点上课，老俞在北京某个会议室开会，我们的这两个动作时同时发生的，但是，这两个动作有任何相同的地方么？<br />
正确选项是选E：过渡性的。为什么过渡性的就相同了呢？比如，从A状态过渡到B状态，这两个状态的过渡阶段是二者共有的。这个共有的状态就是相同状态。<br />
