(10) Since the author frequently ------ other scholars, his objection to disputes is not only irrelevant but also ------.<br />
(A)&nbsp;&nbsp;supports . . overbearing<br />
(B)&nbsp;&nbsp;provokes . . frightening<br />
(C)&nbsp;&nbsp;quotes . . curious<br />
(D)&nbsp;&nbsp;ignores . . peevish<br />
(E)&nbsp;&nbsp;attacks . . surprising<br />
1、从since这个单词判断出该题是因果关系的范畴；<br />
2、后半句表示结果。他反对争吵不仅是不正常的（irrelevant也可以表示不符合其性格）而且是怎么样的。Not only…but also表并列，所以我们只需要通过irrelevant就可以做题。结果是反对争吵不正常，那么原因肯定是他喜欢争吵了。即：因为他喜欢争吵，所以他反对争吵是不正常的。于是B、E候选。<br />
3、看第二个空，不仅不正常（或者不符合其性格）而且怎么样？B是让人恐惧，E是让人惊讶，明显选E更好。<br />
这里涉及到一个我们在上一节课说的BEST的问题。ETS对填空题的官方direction中是这样说的：Choose the word or set of words that, when inserted in the sentence, best fits the meaning of the sentence as a whole. 就是说，有时候要选的选项，不是正确的选项，而是最好的选项。这在这道题中有所体现。其实，B和E都是可以的。当一个人做出与其常规不符合的行为的时候，确实可能会让人感觉害怕的。比如，我上大学的时候有个哥们，一开始不知道多么忠党爱国，都在墙上贴了腊肉的画像，一本红宝书（毛主席语录，不是GRE词汇那个）随身携带的。后来突然有一天，此人回寝室高喊打倒共产党，说蒋公剿匪不力，我们都吓死了，一开始还以为他是被某组织收买了来引蛇出洞的，什么都不敢说。后来证实是因为他偶然看到了一些“反动”书籍。但是，在B和E都可以的情况下，我们要选择一个更好的选项，明显是E了。<br />
