1、从in that可以看出这个词组后面的内容实际上就是解释前面的内容，所以我们只需要读懂后半句的意思<br />
2、后半句说提供了生产其他元素的“建筑材料”。那么答案很简单了，选择E：基本的，基础的。<br />
