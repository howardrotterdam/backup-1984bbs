5、递进转折关系<br />
什么叫递进转折呢？简单地说，就是用表示转折的词语来表示一个递进关系。什么意思呢？举个例子：尽管你知道我很胖，但是你却没想到我有这么胖。其实，这种题目骨子里也是转折，但是是程度上的转折，而不是感情色彩上的转折。比如，大胖，小胖；大革命，小革命等等。<br />
