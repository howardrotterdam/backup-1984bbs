(D)&nbsp;&nbsp;indifference . . followers<br />
(E)&nbsp;&nbsp;approbation . . precursors<br />
这道题比较有难度。我们判断between ------ and condescension就可以看出这道题第一个空应该选一个condescension的反义词。关键是condescension这个词很多英汉词典会解释成屈尊俯就的，在中文里都比较生僻，有点难判断感情色彩…一般来说，当英汉词典不能准确的解释出意思的时候，我们应该马上去查英英的美语辞典，当仁不让的就是merrian-webster的大学生辞典了。这个被认为是ETS出题的官方用辞典。<br />
这个词的英英解释是：a communication that indicates lack of respect by patronizing the recipient. 好了，我们可以判断出这个词是贬义的。那么第一个空应该填一个褒义的词。于是留下了B和E。下面看第二个空。说后世的作家们，比之于她的绝大多数文学（空格）的人，对其作品推崇的程度要来得更高。<br />
B是同时代的人，E是前辈。那么很好选了，明显是同时代的人才会评价的，前辈都死翘翘了，诈尸起来评论？<br />
<br />
7、时间对比关系<br />
时间对比在ETS的考试中用处广泛。比如GRE和SAT的阅读。在阅读里，之前的观点一定会被作者否定掉。ETS总是达拉斯小牛队- -<br />
那么在填空中，时间对比关系可以被这样理解：1，时间相反一切相反，就是说一个时间点之前的状态和这个时间点之后的状态一定是互为反义的；2，一个时间点之前有一种看法，那么这个看法在后来一定会被否定。<br />
关键词：before, until, since, previous, current, recently, now, today, yesterday, later, future, begin, start, once, origin...<br />
<br />
(30) The garden that had remained ------ for months was now pleasantly enlivened by the budding shoots of its perennial flowers.<br />
(A)&nbsp;&nbsp;redolent&nbsp; &nbsp; (B)&nbsp;&nbsp;dormant&nbsp; &nbsp; (C)&nbsp;&nbsp;exuberant<br />
(D)&nbsp;&nbsp;compliant&nbsp; &nbsp; (E)&nbsp;&nbsp;trenchant<br />
最简单的一道时间对比关系题。空格几个月了，然后现在pleasantly enliven，现在生机勃勃，以前肯定是死气沉沉了。于是选一个B。<br />
<br />
(31) The idealized paintings of nature produced in the eighteenth century are evidence that the medieval ------ natural settings had been ------ and that the outdoors now could be enjoyed without trepidation.<br />
(A)&nbsp;&nbsp;fear of . . exorcised<br />
(B)&nbsp;&nbsp;concerns about . . regained<br />
(C)&nbsp;&nbsp;affection for . . surmounted<br />
(D)&nbsp;&nbsp;disinterest in . . alleviated<br />
(E)&nbsp;&nbsp;enthusiasm for . . confronted<br />
这道题的时间对比关键词是medieval和now。中世纪和现在，时间对比，一定取反。现在的状态呢？是could be enjoyed without trepidation。现在可以不用害怕的去玩了，以前呢？不就是害怕么。所以第一个空就填一个A：fear of就可以了。后面不用看了。<br />
<br />
(32) Salazar’s presence in the group was so ------ the others that they lost most of their earlier ------; failure, for them, became all but unthinkable.<br />
(A)&nbsp;&nbsp;reassuring to . . trepidation<br />
(B)&nbsp;&nbsp;unnoticed by . . curiosity<br />
(C)&nbsp;&nbsp;unusual to . . harmony<br />
(D)&nbsp;&nbsp;endearing to . .&nbsp;&nbsp;confidence<br />
(E)&nbsp;&nbsp;unexpected by . . exhilaration<br />
这道题有一定的难度。因为不算非常的直接，需要我们拐一下弯。首先，我们从earlier可以看出这道题是一道时间对比关系题。后面说失败对他们来说，变得几乎无法想象，那就是说他们不会失败了。然后前面说他们失去了之前的空格。空格里明显要填一种态度或者情绪。那么：现在不会失败了，那之前就是要失败了，知道自己要失败了会是怎样一种心情呢？再接再厉？永不言败？这都是发散…选个A就可以了，恐惧，害怕。<br />
翻译：萨拉渣在该团体中的存在对其他人是如此莫大的安慰鼓励，以到于他们此前的大部分恐慌心理已荡然无存；失败，对他们来说，已几近无法想象。<br />
<br />
(33) Although Johnson's and Smith's initial fascination with the fortunes of those jockeying for power in the law firm ------ after a few months, the two paid sufficient attention to determine who their lunch partners should be.<br />
(A)&nbsp;&nbsp;revived&nbsp; &nbsp; (B)&nbsp;&nbsp;emerged&nbsp; &nbsp; (C)&nbsp;&nbsp;intensified<br />
(D)&nbsp;&nbsp;flagged&nbsp; &nbsp; (E)&nbsp;&nbsp;persisted<br />
这道题要做是很简单的，但是句子写得无比恶心。我们先来做一下：从initial（最初的，比如initial-D就是头文字D）和after a few months可以看出这是一道时间对比关系题。initial fascination，一开始有兴趣，那么几个月之后呢？明显就是没有兴趣了。于是选一个D：衰退了。<br />
这句话的翻译是：尽管约翰逊和史密斯早期对于在律师事务所中追求权利的人的命运的兴趣在几个月之后衰退了，但他们给予了足够的注意去决定谁是他们的午餐伙伴。<br />
明显是看不懂的。不要紧，去网上搜索老罗语录-官场论，就能完美理解这道题了。传送门：<a href="http://www.tudou.com/programs/view/ob_9SpOkY1g/" target="_blank">http://www.tudou.com/programs/view/ob_9SpOkY1g/</a><br />
<br />
(34) Before about 1960, virtually all accounts of evolution assumed most adaptation to be a product of selection at the level of populations; recent studies of evolution , however , have found no ------ this ------ view of selection.<br />
(A)&nbsp;&nbsp;departures from . . controversial<br />
(B)&nbsp;&nbsp;basis for . . pervasive<br />
(C)&nbsp;&nbsp;bias toward . . unchallenged<br />
(D)&nbsp;&nbsp;precursors of . . innovative<br />
(E)&nbsp;&nbsp;criticisms . . renowned<br />
约在1960年之前,几乎所有关于生物进化的解释都假设，bla，bla，bla….. 接下来这句话看起来狂专业，读不懂算了。重复：this view是哪个观点？不就是1960年之前没看懂的观点么。1960年之前有这么个观点，现在的研究是肯定还是否定？否定。<br />
A 发现没有偏离这个观点<br />
B 发现这个观点没有依据<br />
C 没有偏见<br />
D 没有预兆<br />
E 没有批评<br />
于是选一个B<br />
