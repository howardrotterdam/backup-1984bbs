2、目的、手段、结果关系<br />
什么叫做目的、手段、结果关系呢？通俗而言，比如，我想要减肥了，这个是我的目的，那么我会用各种方法来减肥，比如节食、运动、药物等等，这些就是手段。最后的结果呢？就是我成功减肥了。那么在GRE填空中，情况也类似。给了你一个结果，你就填上手段，给了你一个手段，你就填上那个预期的结果。所以从骨子里来说，这个关系和因果关系是差不多的，仅仅是因为用的判断关键词不一样所以我把它独立出来了。<br />
这个关系的一些关键词是：in order to, to, for, by, lead to, through, in terms of, on basis of等等。<br />
在这个关系中，还需要注意一个问题：比如，在现实生活中，我要减肥，我也付出了，但是结果是不是一定成功呢？明显是不一定的。但是在GRE的填空里面，这个永远不用考虑的，做了什么，就一定会达到相应的结果。我们来看例题：<br />
(12) By diversity himself of all regalities, the former king ------ the consideration that customarily <br />
protects monarchs.<br />
(A)&nbsp;&nbsp;merited&nbsp; &nbsp; (B)&nbsp;&nbsp;forfeited&nbsp; &nbsp; (C)&nbsp;&nbsp;debased<br />
(D)&nbsp;&nbsp;concealed&nbsp; &nbsp; (E)&nbsp;&nbsp;extended<br />
这是一道比较简单的目的、手段、结果关系题。我们注意到by这个单词，那么很显然，这是一道目的、手段、结果关系题。By后面明显是手段，手段是：剥夺掉自己所有的主权，那么结果呢？怎么样了传统的保护国王的考虑因素？明显是也没有了。于是这道题选B。<br />
