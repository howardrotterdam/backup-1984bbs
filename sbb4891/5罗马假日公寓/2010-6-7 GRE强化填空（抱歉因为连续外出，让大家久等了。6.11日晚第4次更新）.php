4、递进关系<br />
这种关系，顾名思义，就是一个程度的问题。比如，前半句说我很胖，后半句说他更胖。这就是一个递进关系。当然GRE肯定不可能考这么简单的递进，不过不管怎么递进，原理都是一样的：只要判断出是递进关系，那么要是空格在后半句就填一个前半句的状态的递进，要是空格在前半句就填一个比后半句的状态程度轻一些的词就可以了。<br />
判断递进关系的关键词：even, indeed, not only…but also…, almost…<br />
(20) In the seventeenth century, direct flouting of a generally accepted system of values was regarded as ------, even as a sign of madness.<br />
(A)&nbsp;&nbsp;adventurous&nbsp; &nbsp; (B)&nbsp;&nbsp;frivolous&nbsp; &nbsp; (C)&nbsp;&nbsp;willful<br />
&nbsp; &nbsp;&nbsp; &nbsp; (D)&nbsp;&nbsp;impermissible&nbsp; &nbsp; (E)&nbsp;&nbsp;irrational<br />
看到even，那么就可以判断前半句和后半句构成递进关系。前半句是说对一整套被人们普遍公认的价值体系表示直接的蔑视是（空格）的，后面说甚至被认为是疯狂的标志。好，后半句告诉了我们一个疯狂的状态，那前半句既然要和后半句构成递进，前半句就应该填一个比后半句程度轻一些的状态。BCD可以直接排除，关键是AE。A是冒险的，E是不理性的，我们认为E是正确的。为什么呢？因为，疯狂本来就是一种不理性到行为，而冒险则是理性的行为，疯狂和冒险两个词在感情色彩上就是不符合的。有同学认为冒险是不理性的行为，但是冒险恰恰是经过了深思熟虑之后才做的。比如我要去某地探险，一定是考虑了再三，并且看了地图做好了准备买好了一切装备才去的，而不是脑子冲动直接一个人啥也没有冲过去。<br />
而且，就算冒险是不理性的行为，那是“冒险”直接还是“不理性”直接呢？<br />
