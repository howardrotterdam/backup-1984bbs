原则2：遇到两个空的题目，没有硬性的规定是要先分析哪个空的，怎样方便怎样来。另外，大多数有两个空的填空题都是这样做的：先分析一个空，排掉几个，再分析一个空，排掉几个，最后留下的一个就是正确答案。<br />
(3) Just as astrology was for centuries ------ faith, countering the strength of established churches, so today believing in astrology is an act of ------ the professional sciences.<br />
(A)&nbsp;&nbsp;an individual . . rebellion by<br />
(B)&nbsp;&nbsp;an accepted . . antagonism toward<br />
(C)&nbsp;&nbsp;an underground . . defiance against<br />
(D)&nbsp;&nbsp;a heretical . . support for<br />
(E)&nbsp;&nbsp;an unknown . . concern about<br />
1、分析前半句，从countering the strength of established churches可以看出astrology（占星术）这东西是反对国立教堂的力量的，既然反对教堂的力量，那么第一个空应该填一个能够表示“反对教堂力量”的选项。A：独立的信仰，不对；B：被接受的信仰，不对；C：地下的信仰，候选；D：异端邪说的信仰，候选；E：未知的信仰，不对。因此留下了CD两个选项。<br />
2、分析后半句。从just as（就好比）和so today（因此今天…）可以看出后半句要表达的内容和前半句是一样的，就是前半句怎么样后半句还是怎么样。前半句是反对，后半句也是反对了。于是，排除了D，正确选项是C。<br />
其实，这道题从第二个空格入手也是完全可以的。因为我们不用填第一个空已经知道了占星术是反对国立教堂的力量的，所以可以先在第二个空里找一个“反对”的选项…<br />
<br />
原则3：感情色彩在某些时候可以极大的帮助我们做题。<br />
在GRE填空中的感情色彩，其实不需要判断得多么精确，一般只需要看出大是大非就可以了。什么意思呢，就是我们只需要看出某个信息的感情色彩是正面还是负面就可以了。这样足够让我们来做题了。<br />
(4) The disjunction between educational objective that stress independence and individuality and those that emphasize obedience to rules and cooperation with others reflects a ------ that arise from the values on which these objectives are based.<br />
(A)&nbsp;&nbsp;conflict&nbsp; &nbsp; (B)&nbsp;&nbsp;redundancy&nbsp; &nbsp; (C)&nbsp;&nbsp;gain<br />
&nbsp; &nbsp;&nbsp; &nbsp; (D)&nbsp;&nbsp;predictability&nbsp; &nbsp; (E)&nbsp;&nbsp;wisdom<br />
这道题很恐怖。句子很长并且除了最后的句号中间没有一个标点符号。GRE考试的试卷是分成2栏的，意思是，这个句子在考试中是写在试卷的左半边，有7行。看着头晕，其实我们稍微看一下句子的结构就可以看出，这个句子在介绍一个disjunction（分裂），什么分裂呢？between...and…介绍的就是分裂的双方，但是我们是完全可以不用关注这个的。我们只需要知道，两个东西在搞分裂，然后呢，reflect了一个空格。换句话说，主语是The disjunction，谓语动词是reflects，这个句子的主干就是：The disjunction reflects a ------. 就可以做题了。但是这道题的难度还没有结束。<br />
这道题真正的难度在于：分裂会导致什么呢？一般难度的填空题，应该会有一个重复来提示应该选什么，但是这道题什么都没有。那么我们就需要通过感情色彩来判断了。我们前面说过只需要看出大是大非。Disjunction，分裂，在感情色彩来说是一个负面态度，于是我们要选的选项也只能是一个负面态度，因为负面态度导致的东西也肯定是负面态度嘛。五个选项中能表示负面态度的只有A：冲突。其实这句话的意思说白了就是：分裂导致冲突。这道题说的是教育的分裂，不管怎么换，春哥和曾哥的分裂也好，敏感词和人民的分裂也好，能选的也只能是冲突。<br />
实际上，在ETS出的最难的那部分填空题里，往往都不能用常规的做法来做，大多数题目都是猜对的，不是做对的。这个在我们以后讲难题的时候会有体现。<br />
<br />
原则4：注意ETS出题的职业原则，即：什么样的职业只会干什么样的事情，老师只会教书，司机只会开车，等等。<br />
