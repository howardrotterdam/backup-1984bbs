(26) It comes as no surprise that societies have codes of behavior; the character of the codes, on the other hand, can often be ------.<br />
(A)&nbsp;&nbsp;predictable&nbsp; &nbsp; (B)&nbsp;&nbsp;unexpected&nbsp; &nbsp; (C)&nbsp;&nbsp;admirable<br />
(D)&nbsp;&nbsp;explicit&nbsp; &nbsp; (E)&nbsp;&nbsp;confusing<br />
这道题从on the other hand就可以得知是对比关系。哪两者对比呢？codes和character of the codes的对比，即准则和准则特性的对比。什么意思呢？比如，任何一个国家都有交通规则，这个就是codes，但是中国的交通规则和日本的交通规则是不一样的，比如中国是靠右行驶，日本式靠左行驶，这个就是character of the codes，这是不一样的。这道题说：任何社会都有行为准则，这是没什么惊讶的。但是准则的特性，从另一个方面说，是怎么样的。既然是对比关系，那肯定就是填惊讶的了。于是选一个B就可以了。<br />
这道题的E选项有一些争议。要反驳很简单：困惑和惊讶是一回事么？明显不是。脑子要清醒。其实用常识也可以反驳：比如我去日本要开车，然后别人告诉我车子要靠左行驶，我会什么反应？惊讶一下，恩？原来是这样啊，然后开着车上路去了。难道我还会边开车边痛苦的思索，为什么日本的车要靠左行驶么？我又不是交通问题专家。<br />
