(11) During periods of social and cultural stability, many art academies are so firmly controlled by ------ that all real creative work must be done by the ------.<br />
(A)&nbsp;&nbsp;dogmatists . . disenfranchised<br />
(B)&nbsp;&nbsp;managers . . reactionaries<br />
(C)&nbsp;&nbsp;reformers . . dissatisfied <br />
(D)&nbsp;&nbsp;impostors . . academicians<br />
(E)&nbsp;&nbsp;specialists . . elite<br />
这道题在因果关系里算是比较难的。关键是要判断出题目里的逻辑关系。我们从that这个词可以判断出它是一个因果关系。就是说，因为很多艺术学院被牢牢控制的谁的手中，所以真正的创新工作必须由谁来完成。这个题目谈论了创新工作，根据“有因必有果”的原则，所以创新工作，那因为呢？肯定也是创新工作了。也就是说，第一个空必须填一个和创新有关的东西，不然无法构成因果。那么：A，教条主义者，是反对创新的，但是和创新有关（只要对创新有所态度都是有关的）；B，管理人员，和创新无关（不能胡思乱想，认为什么管理人员应该创新什么的。职业原则：管理人员只会管理）；C，创新的人，候选；D，骗子，和创新无关；E：砖家，和创新无关，留下A，C候选。<br />
看第二个空。A是剥夺权利的人，C是不满意的人。好像和第一个空的关系都不密切…不过还好这个句子的大前提是在社会和文化的稳定时期，那么很显然，根据“BSET”原则，艺术学院更有可能被控制在教条主义者手里，因为是稳定时期嘛。<br />
