(21) Few of us take the pains to study our cherished convictions; indeed, we almost have a natural ----- doing so.<br />
(A)&nbsp;&nbsp;aptitude for&nbsp; &nbsp; (B)&nbsp;&nbsp;repugnance to&nbsp; &nbsp; (C)&nbsp;&nbsp;interest in<br />
(B)&nbsp;&nbsp;ignorance of&nbsp; &nbsp; (E)&nbsp;&nbsp;reaction after<br />
这道题本身不难，但是句子写得非常有意义。<br />
看到indeed就知道这道题是一个递进关系。前半句说我们中间很少有人会去费力研究那些我们珍爱的信念，其实就是我们不去研究那些珍爱的信念。后半句既然是递进，那么后半句肯定就是更不去研究了。哪个可以表示“更”不去研究呢？很明显答案是B，不仅不去研究，还反感这么做。<br />
但是这道题可以让我们联想到一些社会上的怪现象。比如，上大学的时候我有时候会找人说，我对达赖的看法。然后边上马上就会有人说：“达赖这个人有啥好谈的，就是个反动分裂分子”；那我想不光是达赖，换个其他的敏感词他们也会这样吧。其实洗脑的威力是可怕的，洗脑可以把一个本来可以被分析的话题（其实哪个话题不可以被分析呢）变成一种下意识的、本能的行为。一个从小看新闻联播而不去思考的人就会把共产党好，敏感词万岁这样的思想变成一种本能，别人和他要稍微讨论一下就会被说成是“反动”，“特务”。这不能不说是很悲哀的事情。<br />
