(22) Despite assorted effusions to the contrary, there is no necessary link between scientific skill and humanism, and, quite possibly, there may be something of a ------ between them.<br />
(A)&nbsp;&nbsp;generality&nbsp; &nbsp; (B)&nbsp;&nbsp;fusion&nbsp; &nbsp; (C)&nbsp;&nbsp;congruity<br />
(D)&nbsp;&nbsp;dichotomy&nbsp; &nbsp; (E)&nbsp;&nbsp;reciprocity<br />
这道题的难点有二：一是逻辑关系的判断，二是词汇。逻辑关系很有迷惑性，因为句子开头有个despite. 于是很多同学会想成转折关系。但是我们把句子读完后，发现考察的却并不是转折关系：尽管众说纷纭，但是科学和人文之间是没有必然的联系的，并且，非常可能地，两者之间还有一定程度上的（空格）。转折关系在“并且”之前已经结束了，之后则是递进关系：没有联系，并且，很有可能的，那肯定是比“没有联系”还要更进一步的了。现在我们知道要填什么了。<br />
但是这道题难度还没有结束。因为判断出了要填什么，单词却比较难。这个…只有去背红宝书了，没什么好说的。选一个D：分裂的。前面是没什么联系，更进一步就是他们二者还是分裂的。<br />
这道题什么意思呢？有些人认为，以及共产党的宣传，都是说什么科学是要服务社会的，是为了让生活更美好。不过这只是一种理想主义的看法。当然不排除那些有社会责任感的科学家，但是，有很多科学家是活的很自我的。他们搞研究只是因为自己喜欢，而研究出来的成果，不要说造福社会了，就算是完完全全的违背社会准则和人文需求，都无所谓。<br />
