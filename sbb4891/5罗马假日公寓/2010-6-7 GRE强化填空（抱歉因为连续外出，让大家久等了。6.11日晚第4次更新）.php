注意：第一个单词，albeit，表示的意思和although是一模一样的，只不过albeit非常正式和书面，口语中是不会使用这个单词的。<br />
(16) Despite the fact that the two council members belonged to different political parties, they ------ the issue of how to finance the town debt.<br />
