<br />
具体的解题方法<br />
从严格意义上来说，方法只有两种。当然对付少数特殊的题目我们还是需要一些非常规的方法，这个现在不谈。<br />
第一种就是找到逻辑关系，一般来说，题干中给了你一个信息，然后通过一些逻辑关键词来判断句子中体现的逻辑关系，然后根据这个逻辑关系选择正确的选项。第二种就是重复，这个我们今天也不讲，不过可以先给大家一个对重复的大致的认识。其实很简单，引用老罗语录，就是这么一句话：“今年的联欢会突出了联欢两个字。”―倪萍语。这就是GRE的重复。<br />
<br />
逻辑关系的判断与解法<br />
1、因果关系<br />
因果关系是GRE填空中很经典的一种关系。<br />
因果关系可以通过以下关键词来判断：<br />
because, because of, for, as, since, in that, be due to, given, considering, as long as, if, when, while, so, so that, therefore, thereby, as a result, consequently, according, thus, so...that..., so...as to, them...<br />
一般来说，出现了这些关键词的句子在多数情况下会是因果关系的句子，但是也不能一概而论。比如有一道题以because开头，但是考的却是反义重复的关系。不过，这仅仅是少数情况而已。<br />
因果关系的解法也比较简单。抓住有因必有果这个原理就好了。比如，因为我饿了，所以我就要去吃饭；甚至，一些题目的因果关系可以考得很脑残，比如：因为我饿了，所以我饿了。这句话说得非常正确，但是它却是一句废话。GRE考试中，很多句子本身是写的很有意义的，但是，还有很多句子写出来就是一句废话。注意，因果关系考的大多数很直接，也仅仅会考常识性的因果，诸如一些什么“因为共产党统治了中国，所以中国人民生活在水深火热之中”这样的设计背景知识的因果关系是不会考的。<br />
(8) As long as nations cannot themselves accumulate enough physical power to dominate all others, they must depend on ------.<br />
(A)&nbsp;&nbsp;allies&nbsp; &nbsp; (B)&nbsp;&nbsp;resources&nbsp; &nbsp; (C)&nbsp;&nbsp;freedom<br />
(D)&nbsp;&nbsp;education&nbsp; &nbsp; (E)&nbsp;&nbsp;self-determination<br />
这是一道较为简单的因果关系题。在这道题中，我们可以从as long as这个词组判断出该题是一道因果关系题。前面说因为国家自身（themselves）不能积累足够的力量来统治其他国家，所以，他们必须依靠什么呢？很明显是联盟了。答案选一个A。<br />
