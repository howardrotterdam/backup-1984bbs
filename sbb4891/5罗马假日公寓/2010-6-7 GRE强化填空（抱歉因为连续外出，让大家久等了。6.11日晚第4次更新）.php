(25) Rather than enhancing a country's security, the successful development of nuclear weapons could serve at first to increase that country's ------.<br />
(A)&nbsp;&nbsp;boldness&nbsp; &nbsp; (B)&nbsp;&nbsp;influence&nbsp; &nbsp; (C)&nbsp;&nbsp;responsibility<br />
(D)&nbsp;&nbsp;moderation&nbsp; &nbsp; (E)&nbsp;&nbsp;vulnerability<br />
这道题其实很简单其实，但是被ETS放在第七题。关键就是这道题出得非常有迷惑性，而且讨论的是核武器这个全世界都关心的话题。从rather than就可以看出来是对比关系。发展核武器非但不能增加一个国家的安全，反而会使这个国家怎么样。既然是对比，那肯定就是不安全了。选一个E：易受攻击的。<br />
但是这道题脑残就脑残在BC两个选项上…中国孩子可能一下不注意就选了B。为什么呢？我们从小就被灌输了一种这样的思想：是两弹一星让中国强大，有了原子弹，美帝和苏修才不敢来侵犯我们。小时候还被组织去看这个纪录片，当时电影院充斥着罗京和李修平那亢奋的声音，看的我们热泪盈眶啊…中国学生用了背景知识，一定选B。<br />
美国学生选C选得多，因为美国人对原子弹的态度普遍不太好，所以他们自然会认为一个国家有了原子弹就要负有更多的责任去保卫世界不被核武器所破坏。<br />
其实，集权国家搞原子弹的目的，其实就是为了保卫领导人自身吧…一旦某个独裁者有了原子弹，他马上就可以乱来了…毛泽东…金正日…<br />
