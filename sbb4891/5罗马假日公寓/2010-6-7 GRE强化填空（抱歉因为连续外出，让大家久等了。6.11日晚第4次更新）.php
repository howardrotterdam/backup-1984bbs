(9) The results of the experiments performed by Elizabeth Hazen and Rachel Brown were ------ not only because these results challenged old assumptions but also because they called the ------ methodology into question.<br />
(A)&nbsp;&nbsp;provocative . . prevailing<br />
(B)&nbsp;&nbsp;predictable . . contemporary<br />
(C)&nbsp;&nbsp;inconclusive . . traditional<br />
(D)&nbsp;&nbsp;intriguing . . projected<br />
(E)&nbsp;&nbsp;specious . . original<br />
这道题的题干比较长，不过关键字还是很好找。首先前面说的是结果（The results），后面说的是原因（not only because…）。后面的原因是：challenged old assumptions和call…into question. 挑战了什么什么，质疑了（call into question）什么什么。因为结果又挑战又质疑，所以第一个空明显填一个要么是挑战的，要么是质疑的选项了。于是选一个A，第二个空不用看了。<br />
这个句子写得很脑残：因为结果挑战了，所以结果挑战了。<br />
