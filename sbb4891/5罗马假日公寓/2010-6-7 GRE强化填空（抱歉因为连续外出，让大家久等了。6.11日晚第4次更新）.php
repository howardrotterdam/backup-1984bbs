													<h2>GRE强化填空（抱歉因为连续外出，让大家久等了。6.11日晚第4次更新）</h2>
						
						
																											<div id="postmessage_660886" class="t_msgfont">非常抱歉各位。这两天连续外出讲座，没空更新。好在讲座已经结束了，可以写一点内容。大家久等了。下午先小幅更新一下，晚上再更新一次。<br />
<br />
首先当然是要感谢国家。<br />
然后简单说明几点：在小组内讲GRE和SAT的课程，是我自愿的行为，并且该行为是无偿的。这个过程得到了张书记的支持，在此表示感谢。我只是希望我的课程能够给予要考GRE的同学一些帮助。课程还是采取最原始的打字方式....没有视频也没有语音.....不过我可以保证的一点就是：我的这些东西会比市面上能够找到的任何一本GRE填空或SAT阅读的参考书都要好。我尽量把语言写得通俗易懂，不会像某些参考书那样故弄玄虚，装模作样。今天更新GRE填空的第一部分，GRE填空将尽量保证每天更新。SAT阅读将于明天或者后天开始讲。<br />
<br />
另外：请大家尽量不要把这些东西放到别的网站上去，让它在小组内部流传就好了。谢谢。<br />
<br />
GRE填空<br />
在开始我们的课程之前，我首选需要大家了解一些关于GRE考试和填空的基本知识。<br />
首先，GRE考试是一个逻辑考试，理论上，它是不考语言水平的。但是这个考试面对的是本科学生，所以自然的，里面的句子、文章也必然会比较“学术”，通俗的说，就是晦涩。单词，也是高级英语单词。所以，要考GRE，前提需要一定的英语基础，一般来说，4级就可以，6级就更好了，并需要背下“红宝书”或者“蓝宝书”。这是考好GRE的前提条件。<br />
另外，始终要记住，这个考试，骨子里还是一个逻辑考试。所以，有了单词量这是基础，但是要得高分，还是要针对其题型进行专门的训练。于是就有了我的这个帖子。这个课程，主要讲GRE填空。<br />
<br />
GRE填空，出现在GRE考试的Verbal（语文）部分。一个Verbal部分一共有38道题，考试时间30分钟，一次GRE考试一共有两个Verbal部分。填空是首先出现的，每个Verbal前7题是填空，也就是说一场GRE考试里有14道填空。难度基本上按照由易到难的顺序分布。<br />
<br />
GRE填空，说是说填空，但是和传统意义上的中国填空题不一样。中国的考试，一般选择题和填空题是不一样的，就是说，选择题是4选1，填空题是挖个空然后给你自己填上个什么东西。但是GRE的填空题是以选择题的形式出现，5选1，通俗的说，就是：一个句子挖掉一个或者两个空，然后要你填一个或者两个单词进去，使其逻辑通顺，最符合句意。<br />
<br />
下面来看一下ETS（GRE出题机构的名称的简写）的官方Directions：<br />
Each sentence below has one or two blanks, each blank indicating that something has been omitted. Beneath the sentence are five words or sets of words labeled A through E. Choose the word or set of words that, when inserted in the sentence, best fits the meaning of the sentence as a whole.<br />
<br />
这段话大家肯定都看得懂，唯一需要注意的是第三行的best这个单词。但是现在大家不用太过关心它，我们以后会说到。<br />
<br />
如果单论题目表面的长相，和GRE填空最接近的是中国英语考试的选择题了。一样是一个句子，一样是挖掉一两个空，要选择单词进去。那么，GRE的填空到底和中国英语考试的选择题有什么不同呢？举两个例子大家就知道了。在这里为了便于大家理解，这两道题我均用中文出出来，这样可以给大家一个更好的认识。<br />
<br />
例1：1984bbs的张书记是------。<br />
(A)&nbsp;&nbsp;政府干部&nbsp; &nbsp; (B)&nbsp;&nbsp;爱党分子&nbsp; &nbsp; (C)&nbsp;&nbsp;国保<br />
(D)&nbsp;&nbsp;五毛&nbsp; &nbsp; (E)&nbsp;&nbsp;1984bbs的创始人<br />
这是一道典型的中国式选择题。特点是：我们有在知道背景知识的前提下，才能够正确选择，否则无从做起。下面我们来看一道GRE形式的填空题。<br />
<br />
例2：毛泽东，这个在中国开展了无数次政治运动，进而------了无数中国人民的韶山人，毫无疑问的一个刽子手，却在中国得到了许多人民的------，这不能不说是一个极大的讽刺。<br />
(A)&nbsp;&nbsp;拯救 . . 憎恨<br />
(B)&nbsp;&nbsp;残害 . . 厌恶<br />
(C)&nbsp;&nbsp;杀死 . . 尊敬<br />
(D)&nbsp;&nbsp;解放 . . 喜爱<br />
(E)&nbsp;&nbsp;进化 . . 唾弃<br />
这道题如果翻译成英文那就是一道很好的GRE试题。这道题谈论了毛泽东。毛泽东，中国人都知道，美国人也许知道，但是不了解，但是无论知道与否，都不妨碍我们顺利的把这道题做出来。首先，“进而------了无数中国人民的韶山人，毫无疑问的一个刽子手”的前后半句毫无疑问是一个重复的关系，也就是“毫无疑问的一个刽子手”是对“进而------了无数中国人民的韶山人”这种行为的一个总结。既然是刽子手，那就是杀人了。于是，看第一个空，ADE可以很简单的排除掉，留下了BC候选。然后来看第二个空。从“却”和“极大的讽刺”这些信息我们可以很容易判断出后半句的感情色彩是和前半句相反的。于是，后半句应该填一个正面感情色彩的选项，那么，C选项就是正确选项。<br />
<br />
这道题其实较好的表现了GRE填空的几个特色。<br />
首先，它是不需要你的背景知识的。不管你对毛泽东的评价是怎样的，这道题的正确选项只有这一个，因为解题线索只能来自“刽子手”和“却”“讽刺”这些信息。<br />
<br />
另外，虽然不需要背景知识，但是我们还需要对某些单词或信息有一些常识性的了解。比如在这道题中，我们需要知道刽子手是一种不好的职业，是杀人的职业。这种常识肯定是没有问题的，GRE考试所要求的“常识了解”基本上也就到这一步了。不过有极少数题目所需要的常识就比较高深了，比如有一道题要求你对bibliographer这个单词有所了解。这个单词很多辞典上的解释就是修书者，书目编撰者，但是这还不够，还需要你了解这个职业到底是干什么的。我个人认为这种“常识”已经超越了一般的“常识”的范畴了，有点背景知识的味道在里面。所以这些极少数的题目，可以认为是烂题。<br />
<br />
另外这道题还涉及到了ETS的职业原则。就是说，在ETS的想法里，什么样的职业只会做什么样的事情，比如一个老师只会去教书，一个刽子手只会去杀人，虽然他们平时完全可以做别的事情，但是完全不用再GRE的填空里去考虑它。<br />
<br />
最后，虽然这道题是比较简单的，但是大家读这个题干的时候，应该心里是不会很爽的。我说的不是内容，是这个句子的写作方式。虽然大家看懂没有问题，但是写作方式却和我们平时说话的方式是大相径庭的，一句话，读起来有点晦涩。这也是GRE填空题的一大特色，句子写得晦涩、恶心。但是个人认为这并不算是一个很大的问题，很多恶心的句子我们依然可以通过对逻辑的把握把题目给做出来。其实，大家只要经过一段时间的训练，就能适应这种恶心的句子了。<br />
<br />
好，相信通过上面的文章，大家对GRE填空已经有了原则上的了解。那么我们现在可以开始讲GRE的真题了。<br />
<br />
首先，我们要讲做GRE填空需要贯穿始终的几个大原则。就是说，对于任何填空题，都不应该违背这些大原则。<br />
<br />
原则1：任何填空题的答案都可以在文章中找到线索，不能凭空臆断，也不能使用背景知识。<br />
(1) The natural balance between prey and predator has been increasingly ------, most frequently by human intervention.<br />
(A)&nbsp;&nbsp;celebrated&nbsp; &nbsp; (B)&nbsp;&nbsp;predicted&nbsp; &nbsp; (C)&nbsp;&nbsp;observed<br />
&nbsp; &nbsp; (D)&nbsp;&nbsp;disturbed&nbsp; &nbsp; (E)&nbsp;&nbsp;questioned<br />
在这道题中，捕食者和被捕食者之间的自然平衡已经越来越被怎么样了，一道生物学范畴的题目。但是是不是学生物的同学就有优势了，或者说不是学生物的同学就没有优势了呢？不是。学生物的同学可能会根据自己学的知识在空格里填上各种答案，比如，越来越增强了，越来越削弱了，越来越不可靠了等等，但是在GRE填空中，这种凭空臆断和背景知识的使用却是大忌。我们必须在句子里找到线索。<br />
正确的解题思路是：1、前半句和后半句之间没有使用提示转折、对比、递进等逻辑关系的连词，因此前半句和后半句之间是一个简单的重复关系。<br />
2、后半句说最频繁的被人类所干扰，那么，既然前后半句是重复关系，前半句必须填一个重复，也就是说填一个“干扰”的重复。因此，这道题选择D：干扰，打扰。<br />
