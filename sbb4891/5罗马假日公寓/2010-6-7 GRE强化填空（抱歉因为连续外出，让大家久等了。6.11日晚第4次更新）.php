6、对比关系<br />
对比关系是GRE填空题中的一大关系。既然是对比，那肯定是两个东西放一起对比了。比如春哥和曾哥放一起对比，他们都是爷们。但是在GRE填空中，注意一个原则：出现了二者对比的，一定是取反。把握到了这个原则，题目就好做了。<br />
对比关系的关键词有：contrast, contrary, far from, on the other hand, instead, rather than, not… but…, not so much…as…, more than…<br />
还要注意一点，某些题目，讲的是A to B或者A from B的一个状态，或者是between A and B的一个状态。那么，A和B也一定是互为反义的。这可以帮助我们很快速的做某些题目。<br />
