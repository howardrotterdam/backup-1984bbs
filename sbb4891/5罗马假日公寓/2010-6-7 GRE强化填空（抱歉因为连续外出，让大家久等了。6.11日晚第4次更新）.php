(13) Edith Wharton sought in her memoir to present herself as having achieved a harmonious wholeness by having ------ the conflicting elements of her life.<br />
(A)&nbsp;&nbsp;affirmed&nbsp; &nbsp; (B)&nbsp;&nbsp;highlighted&nbsp; &nbsp; (C)&nbsp;&nbsp;reconciled<br />
(D)&nbsp;&nbsp;confined&nbsp; &nbsp; (E)&nbsp;&nbsp;indentified<br />
