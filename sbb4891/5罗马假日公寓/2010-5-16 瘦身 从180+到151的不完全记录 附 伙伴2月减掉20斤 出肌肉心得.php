													<h2>瘦身  从180+到151的不完全记录  附 伙伴2月减掉20斤  出肌肉心得</h2>
						
						
																											<div id="postmessage_617778" class="t_msgfont">本人基本情况：<br />
<br />
男，22周岁，07年~09年年初体重稳定在183斤，经常是一顿饭后，体重上下浮动3~4斤。<br />
没有吃早餐的习惯，正常饭量是周围伙伴平均饭量的1倍，运动较少。<br />
<br />
09年3月份~4月份，坚持晚上跑步，400米跑道3圈~5圈，饿了吃黄瓜，晚9点后不吃东西（这个坚持到了现在，偶尔夜宵）<br />
1个多月，体重下降到175左右，稳定了1个月。<br />
<br />
09年5月份~7月份，跑步没坚持下来，但是饮食方面比过去注意了，体重一直在178左右。<br />
<br />
09年7月份~8月份，单车走了一趟川藏，体重曾下探至169<br />
<br />
09年9月份~10年3月份，体重长期在173~175浮动<br />
<br />
10年3月份~现在，按10周算：（健身房期间完成的，晚上7点半~9点后）<br />
<br />
1~2周，每天20分钟跑步机，70分钟左右器械锻炼；没吃早饭，午饭正常，晚饭减半，锻炼完喝奶粉，饿了吃黄瓜苹果。体重瞬间到160+，效果超级明显。<br />
<br />
3~4周，每天30分钟跑步机，70分钟左右器械锻炼，仰卧起坐120个，没吃早饭，午饭正常，晚饭减半，锻炼完喝奶粉，饿了吃苹果（黄瓜贵了），体重到了158，突破160大限度。<br />
<br />
5~7周，每天30分钟跑步机，70分钟左右器械锻炼，仰卧起坐160个，没吃早饭，午饭正常，晚饭减半，锻炼完喝奶粉，饿了吃苹果，一周大概吃2次肉，为了练肌肉，晚上锻炼完吃两个茶叶蛋，体重到了155<br />
<br />
8~10周，每天30分钟跑步机，70分钟左右器械锻炼，仰卧起坐180个，没吃早饭，午饭正常，晚饭减半，锻炼完喝奶粉，饿了吃苹果，一周大概吃2次肉，晚上2蛋，体重到今天是151斤。<br />
<br />
-------------------------------------------------------------<br />
伙伴瘦身：<br />
<br />
同我一起办的卡，开始前是183斤<br />
1~2周，每天20分钟跑步机，仰卧起坐40个，器械70分钟（运动量非常少，有我的4分之1）<br />
<br />
3~4周，每天30分钟跑步机，仰卧起坐100个左右，器械70分钟，（运动量非常少，有我的4分之1，主要是二头肌和胸肌）<br />
<br />
5~6周，每天30分钟跑步机，仰卧起坐120个左右，器械70分钟，（运动量非常少，有我的4分之1，主要是二头肌和胸肌）163斤<br />
<br />
7~10周，每天30分钟跑步机，70分钟乒乓球，仰卧起坐80个，4组胸肌练习，160今天<br />
<br />
具体体重数据没印象，但是前2周，他跟我一样都减下去十几斤，他饮食比较苛刻，每天只一顿饭，偶尔早上吃个面包，晚上吃苹果，当然就没有力气练肌肉，所以一直肌肉效果不明显，后来开始一起晚上吃2蛋，因为体力消耗太大，身体都发虚。<br />
<br />
---------------------------------------------------<br />
健身出肌肉：<br />
参考<a href="http://www.verycd.com/topics/201646/" target="_blank">http://www.verycd.com/topics/201646/</a><br />
<br />
每周，肩膀，胳膊，胸肌各练习3次，没有想出大块头，所以量都是最轻的。<br />
<br />
3周以后，胸肌和肩膀有明显效果，大概是原来胖的时候胸前肉多，好练；<br />
7周以后，2头肌出了效果，因为训练量不大<br />
10周，腹部只是比过去明显瘦了好几圈，皮带先后处理过2次，上身基本上都有凸起效果了，胸肌效果最好，只是依然有肥肉^^。<br />
<br />
练习肌肉的目的主要是为了保持体重稳定，能出点力量感，平时牛肉和蛋白补充的也不够，所以并没有特别明显的地方，不过自己已经很满意了，能坚持3~4个月，上身轮廓就能有个大概了。现在的上肢力量已经有了明显提高，再接再厉。<br />
<br />
想短期出肌肉，除非有良好的基础，还要大量吃牛肉，鸡蛋之类的...<br />
<br />
可以参照上面地址的视频，挺实用的。<br />
<br />
<br />
<br />
<br />
==========================<br />
PS，10周的健身房计划实施并不完美，我中间先停了1周（外出），后又停了8天（5・1假期），这中间各种饮食补给的也是很到位，伙伴坚持的比较好，加一起只缺了7，8天，2人的队伍现在发展到了6人，不过固定的就我俩<br />
<br />
PPS，总结去年到现在的瘦身行为，认为必须结伴，因为我个人控制能力差，结伴可以相互比拼和监督，去年的川藏行让自己狠狠的拉了回体力，健身坚持到现在，都觉得很随意，心态上比较放松；安逸的生活环境比有压力的时候容易发胖，所以健身期间最好能在时间上严格的约束自己，这样能保证整体效果；瘦身道理很简单，保持饮食，增加消耗，坚持即可，一开始不能要求自己太高，否则不容易坚持，一点一点慢慢来，这是个长期过程，猛瘦下来了，可以适当保持一段时间，让身体适应，也避免日后大幅度的反弹，这中间可以大胆的吃肉喝酒^^<br />
