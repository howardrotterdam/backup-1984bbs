													<h2>傻逼领导下如何干活</h2>
						
						
																											<div id="postmessage_515274" class="t_msgfont">首先说明一点，这肯定是指公家单位里，因为私营部门一般不会出现这种情况，原因很简单：对私利的追求决定了老板不会把傻逼放在领导岗位上。<br />
<br />
其次，这里年轻人多，老一点的也可从其言论看出，他们并未掌握在公家单位如何对付傻逼领导的办法，否则他们的网言不会这么愤。所以，我出的这一狠招是给上述两种人的，等你们学会并得到好处了，别忘了感谢我。哈哈。<br />
<br />
那么，在傻逼领导下该怎么干活呢？很简单：比傻逼更傻逼地干。<br />
<br />
傻逼所以能给牛逼当领导，是体制决定的，一般情况下你改变不了――除非你花的钱比他多。所以，这个办法是你无法通过正当手段驱走傻逼的前提下说的。<br />
<br />
傻逼着干的理由如下：<br />
<br />
一，假如你干得好，功劳是傻逼的而不是你的，你干得越好，傻逼在上级眼里就越好，那么傻逼职位保得就越久，甚至可能升迁。所以，你越往好里干，就越等于帮了傻逼忙。<br />
<br />
二，因为是傻逼嘛，所以，即使你干得好，傻逼也看不出来，甚至可能说你干得孬，而且，通常，傻逼的上级也是傻逼，你有理无处说。那你干吗要干好？<br />
<br />
三，干得好一定包括干得多，干多了，出错几率就多，这等于你自寻烦恼自讨苦吃，还给傻逼留下整你的把柄。这就是傻逼领导下的单位总是庸人占便宜的原因。<br />
<br />
四，你（最好多数人）傻逼着干，会有两种情况，1，大家都傻逼没多久，再上一级的领导可能良心发现，说不定就会考虑换掉那个傻逼了，即使不换，一旦露出换的意图，傻逼就得放血，让傻逼损失一把不也值得吗？2，单位烂得实在不像样了，一定会改朝换代，你虽然因为傻逼而受了一些损失，但终于熬到头了（比你干得好而把傻逼送上更高层位强得多）。无论那种情况出现，傻逼受损或走人总是一件爽人的事。<br />
<br />
五，假如新换领导，怎么干活也取决于你对他的观察和判断，假如还是傻逼，那毫无疑问――只能继续傻逼着干。<br />
<br />
六，在傻逼手下你心里肯定不爽，但你要强忍，不要表露，甚至还要恭维傻逼，诱导他越傻逼越好。但这要功夫，我的功夫还没到家，否则早就升了。呵呵。<br />
<br />
七，每个单位总有新手进来，这些人不明就里，往往以为只要干好了就有机会，所以他们会很积极。对于这些人，你就要适当开导开导了。不忍心开导也没关系，因为工作本身也能教会他，只是时间会长一点。<br />
<br />
八，有些人很执着（我不说他笨），兢兢业业工作了很长时间也没得到应有报偿（奖励啊升迁啊），但工作依然干得那么好。值得钦佩吗？确实值得。可恶吗？也实在可恶。原因上面已经说了――也许就是因为有这么一两个或三四个兢兢业业的人，才使傻逼安然无恙甚至飞黄腾达。<br />
<br />
九，假如实在忍不下去又无处可去，最好的办法绝不是跟傻逼对着干，而是自己花钱（运气好有技巧时也不需要太多）去当“傻逼”。<br />
<br />
需要说明的是，说了半天傻逼，其实他们一点儿都不傻，真正的傻逼反而是卖命工作又无相应报偿、气不顺时还卖命的人。希望大家不要做这样的傻逼。<br />
<br />
公家单位所以有这些帕金森定律，原因在于：领导和员工的利益多数情况下不一致。私营单位恰恰相反。所以，假如你在私营单位，那就得拿出真本事了。<br />
<br />
帕金森定律起源于英国，在资本主义国家公家机关里也不同程度地存在。没有这里严重的原因是，那里多少还有个人负责制。<br />
<br />
所以，公字当头下，当一座大厦将要倾倒时，最可恶的就是那些用肩膀去扛的人！正是因为这些人的肩扛，使其他人无视甚至忘却了大厦将倾的危险，最终成了陪葬品。它从反向度证明了“人不为己、天诛地灭”的道理！<br />
<br />
那些兢兢业业的人可能不忍心这么做，觉得这不是搞破坏吗？呵呵，此时你首先要明白一点：让傻逼当领导本身就是破坏！用这种办法驱走傻逼反而是贡献！所谓用野心对付野心、以恶制恶！何况，这样做自己也轻松。可见，人以自利为目的所能对社会的贡献比他意图对社会做的贡献还要大。<br />
<br />
以上就是对付公家单位傻逼领导的办法。嘘，瞧瞧地看，然后不露声色地干。当你如法炮制驱走一个傻逼或让他放血了，自己开心了，别忘了谢我一声。哈哈。<br />
