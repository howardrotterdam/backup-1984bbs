													<h2>哥念大学的时候让所有的室友和班干去喝茶了</h2>
						
						
																											<div id="postmessage_481792" class="t_msgfont">看到不少人在说喝茶，我也讲一讲我关于喝茶的往事。<br />
<br />
不知道大家是否还记得杜导斌先生的被逮捕，那是我上大学的时候，大三吧。那时候网上有一个签名活动，就是请人大解释一下嘛叫煽动颠覆国家政权，哥参加了，并在一个叫槟榔园的坛子里和几个疑似细作的人吵了一架。<br />
<br />
而这个时候，一个香港人通过论坛留下的练习方式找到我――我当时信奉君子坦荡荡，主动实名（SB）――他的网名很怪，有的时候叫温+饱，有的时候叫蒋泽敏<br />
<br />
当时寝室里全是人，如果大谈自由民主专制之类，会被人围观笑话的。于是我基本嗯啊答应，一句话不说。他也没有说什么，只是谈到了关于他所经历的希望工程骗局，好像还说到他是当年逃到香港去的，当时在香港水利部门做工程师。这就是麻烦的诞生。<br />
<br />
与所有不务正业的大学生一样，我善于逃课。那天，正在小猫上网在坛子里发表不和谐言论（当时和谐一词尚未兴起），忽然觉得有人在身后，回头一看是学生处于处长，当时毛了，以为要被处分。没想到他竟然没有深究逃课的事情，而是问了我平时“我们”都用电脑做什么……等等奇怪的问题。<br />
<br />
当时我于该处长侄子交好，经常用他的宽带下载一些天安门之类的大片。下午照例去他们寝室，他们寝室的大爷居然不让我进，终于进去后，我才知道，原来事情闹大了。为了我的事情，分院的有关领导专门驱车去大连总院开了会，当时很害怕，销毁了不和谐的光碟，反复格式化硬盘――填满再格。但是没有人找我喝茶，虽然我很渴，很忐忑。<br />
<br />
不过跟我一个寝室的和旁边寝室的同学以及班干部却开始一个一个的被找去谈话，全是在中午时间，弄得大家吃不上午饭，回来后，全都给我以怨怼的眼神。一个跟我不错的朋友说，到那以后，两个多小时的谈话完全毫无边际，从法轮功谈到祖国统一，从西藏谈到新疆直到台湾岛，基本上把所有可能出问题的地方全部用官方观点洗脑，然后告诉大家要注意我的一举一动和言行，可以向保卫处报道。<br />
<br />
于是，我感受到友情的脆弱，同学都远离我，用异样的眼神看着我。一些入党积极分子还不是时机的挑逗嘲讽以致谩骂，我被隔离于人群之外。<br />
<br />
另一方面，虽然当时是小猫拨号上网，甚至一个5MB的文件没有下完就要重新拨号若干次，但我的电脑仍是经常有木马，赤裸裸的攻击，一联网就让你根本无法打开任何软件，换了多少个版本无济于事。<br />
<br />
当时的我害怕了，一边喝着50度小烧一边写了篇帖子，大意是我追求的民主是社会主义的民主…… 。里边确实有许多观点是当时政治课遗留下来的畸形垃圾。但等我写完发布之后，我哈哈大笑，然后再喝酒，喝伤了，躺在床上两天没有下地。我从此觉得以往的幻想全是狗屁，直到现在，你让我讲一个最短最好笑的笑话，我一定告诉你：“胡温新政”，至此完全对他们不抱任何希望。<br />
<br />
直到最后，我毕业了，他们也没有任何人找我，不过有两次，我的一些同学告诉我，我们那个鸟不拉屎的学校里看到了喷涂有“国安”两个字的车子，不知道是真的，还是编出来故意吓唬我。<br />
<br />
到了今天，除了那个其他年级的学生处处长的侄子，整个大学生涯，我没有剩下一个朋友。<br />
<br />
我说这些的目的是，我要说，喝茶并不可怕，只要是茶叶就能生津败火。最怕的是那种让你隔绝于人群，自绝于人民的孤独感……那真的是下作。<br />
<br />
所以，年轻的朋友，想要做什么的话，请确认自己的心灵足够坚强。</div>

							
							
							
							
