																											<div id="postmessage_442339" class="t_msgfont">国家:丹麦，瑞典，挪威，芬兰<br />
<br />
语言要求: <br />
1, 对于所有本科毕业寻求研究生学位的同学，英语是唯一需要的教学用语言，大部分的正规学校要求申请者持有托福或者雅思的有效语言成绩，根据学校不同，语言要求会有所变化，但是基本上，托福过600，雅思过5.5分是必要的。当然，对于工科专业，我也有见过没有语言成绩的申请成功后再去补考托福雅思。<br />
2, 对于高中生寻求本科学位的小朋友们，一定要注意，所有的欧洲国家本科学位通通是用本地语言授课的，这意味着在开始本科学习之前，留学者必须要接受一年或者更长的语言课，并且通过考试。个人不建议高中之后就出去留学的，因为耗费的时间要远超过在国内读完本科再出国的。我的几个朋友高中毕业后在国外消耗了近6年才拿到本科学位。<br />
<br />
申请流程: <br />
所有的申请流程必须详细参考所申请大学的主页，北欧学校的索引可以在<br />
<a href="http://www.kina.cc" target="_blank">www.kina.cc</a><br />
找到，注意的是，很多学校的申请必须是在线申请。一般而言，北欧的大学一年有两个申请时间，一个是申请秋季入学，一个是申请春季入学，请注意不要错过申请截止日期。<br />
对于高中生申请者，因为很可能需要在当地重新入学一年高中补习课程与语言，所以这个时间并不是很重要。<br />
<br />
个人经验:<br />
北欧国家除芬兰外其他都是日耳曼语系，即是与德语同源。丹麦，瑞典，与挪威语，基本上可以算作德语的三种方言，在书写上并没有太大的差别，但是在读音上几乎没有共同点。需要提醒的是，尽管大部分留学只需要使用英文完成学业，但是掌握当地语言是寻找本地工作和拥有社交的前提。虽然这些国家有很多国际性公司，但是除了工程类以外，其他工作对英文的依赖并没有那么高。在瑞典，一旦企业与员工签订了工作合同，该员工一般是以雇员而不是合同工身份工作，这意味着即使公司要解聘员工，也必须按照对待雇员一样提前3个月以上通知，并负责一定程度的工作推荐。在丹麦，这个解聘同样需要3个月的提前期。<br />
<br />
