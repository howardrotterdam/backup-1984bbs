																											<div id="postmessage_771526" class="t_msgfont">这本书最近很红的样子，很多人都在看。<br />
<br />
找了本大致翻阅了下。<br />
<br />
摘录一段我认为写的不错的：<br />
<br />
搭讪常见错误对话及分析<br />
开场阶段<br />
搭讪犯：“你好。”<br />
MM：“嗯……”<br />
搭讪犯：“刚才在那边看到你，觉得你气质不错，所以就过来认识一下，能把你的电话号码告诉我吗？”<br />
MM：“不要吧……”<br />
分析：<br />
首先，在没有任何交流的情况下，不应该直接要号。<br />
其次，不要上来就赞美，除非你就是来抒发感情的，尤其不要说气质好，这年头赞美姑娘气质好已经是非常过时的行为了。<br />
比较正确的流程：<br />
搭讪犯：“你好……”（停顿一下，让对方有个反应过程）<br />
“我想认识你”或者“其实，我就是想来认识你的。”<br />
MM：“哦……”<br />
搭讪犯：“我刚从那边的音像店出来，看见你正在一个人吃冰激凌，于是我脑袋一热就跑过来了。”<br />
把自己怎么发现MM,然后又怎么上前搭讪的过程简要叙述一遍，通常可以给对方带来最基本的安全感。<br />
而这时候就进行赞美是会影响安全感的（ 显得你有意图），尤其是赞美气质，直接把你打入*小弟的行列。<br />
避免空洞的赞美，但可以把你眼中的她描述得具体一点儿，这样会让MM觉得这个搭讪犯真实且有趣。<br />
<br />
中场阶段<br />
搭讪犯：“你是来逛街的吗？”<br />
MM：“嗯……”<br />
搭讪犯：“那咱们一起逛吧？”<br />
MM：“哦，不用了吧……”<br />
分析：<br />
典型的男人式的废话，明明已经在一起逛着呢，非要多嘴再声明一下，把一件正在自然而然进行的事情变成一个契约，让MM的心理防卫陡增。<br />
不过有趣的是，这句废话的根源反而是男人自己缺乏安全感。<br />
<br />
收尾阶段<br />
搭讪犯：“留个手机号吧”<br />
MM：“好的，1234567www.”<br />
搭讪犯：“你叫什么名字？”<br />
MM：“啊……叫我莎莎吧……”<br />
分析：<br />
有时候，MM愿意给你电话，却不一定愿意马上告诉你她的真实姓名，所以，更恰当的问法是――“我该怎么称呼你？”<br />
<br />
结束语<br />
MM：“拜拜。”<br />
搭讪犯：“拜拜，有空一起出来玩！”<br />
分析：<br />
为什么非要再加上这句“有空一起出来玩”呢？真是狗尾续貂、画蛇添足。<br />
首先，人家只是给你一个相互了解的机会，还没把你当朋友，这句口号会让人觉得你自以为是。<br />
其次，就算是可以出来玩的关系，最好也到时候再说，因为你面对的是女人，不是业务客户。对于客户来说，能一起出来玩是有一定意义的，所以分开时强调一下也罢；而对于女人来说，出来玩的情绪状态才是最重要的，因此提前说也没用，反而显得这个男人婆婆妈妈。<br />
<br />
<br />
