=========================<br />
这个可以让男的学学说话的方法。<br />
<br />
很多男的，碰到喜欢的女性。<br />
要么害羞的一言不发；要么紧张的语无伦次，要么直接的好像想马上和你上床<br />
<br />
EQ不足靠多看看书或许有用，至少可以少说点让别人觉恶心头皮发麻的话。<br />
