													<h2>万邦周末文化沙龙规则</h2>
						
						
																											<div id="postmessage_371110" class="t_msgfont">万邦周末文化沙龙<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; 经万邦文化沙龙2009年10月24日书友讨论通过。<br />
<br />
一、规则：<br />
<br />
A、 自由发言有时性。<br />
<br />
主题发言5-10分钟，自由发言每人3分钟（经主持同意可以延长）。<br />
<br />
B、 集中辩论区。<br />
<br />
在自由发言之后，时间30-60分钟。<br />
<br />
C、 开放互动性。<br />
<br />
会员参加应该有老会员介绍，每期应有50%的新会员参加。<br />
<br />
D、友好辩论原则。<br />
<br />
不得随意打断别人的发言，不得进行人身攻击和使用不文明的语言。<br />
<br />
E、 主题明确性。<br />
<br />
不得跑题，如跑题，主持人可以随时纠正。<br />
<br />
二、程序：<br />
<br />
互相介绍（填写会员登记表）―主持介绍主题和背景―自由发言---集中辩论---小结。<br />
<br />
三、宗旨：<br />
<br />
读书&nbsp; &nbsp;&nbsp;&nbsp;讨论&nbsp; &nbsp;交友<br />
<br />
四、会网互动：<br />
<br />
经新浪“启蒙部落”博客圈发布相关信息。<br />
<br />
1、&nbsp;&nbsp;提前发布议题及背景资料。<br />
<br />
2、&nbsp;&nbsp;观点网上发布并公布支持率和反对率（发布统计数据）。<br />
<br />
3、&nbsp;&nbsp;建立书友QQ群和推广新浪的“启蒙部落”博客圈，通过网络加强沙龙的影响。<br />
<br />
五、时间及地点<br />
<br />
每周六下午2：30―4：30（个别时间可以调到星期天，以万邦书城工作人员的短信通知为准）。<br />
<br />
万邦小寨店高士茶坊。<br />
