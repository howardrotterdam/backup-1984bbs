													<h2>岁末万邦读书会月月开讲田刚“鲁迅与中国自由主义”</h2>
						
						
																											<div id="postmessage_372067" class="t_msgfont">新浪博客圈启蒙部落圈内各友：<br />
&nbsp; &nbsp; 由我们策划的“万邦读书会每月讲堂”已初获成功！这让我们这些组织者在倍受鼓舞的同时，也深感责任重大。<br />
&nbsp; &nbsp; 当今的中国民怨沸腾，给新老“左派”余毒的释放提供了良机，同时随着中国经济的市场化越来越大，自由民主人权等人类普世价值更加深入人心；在网上有关“燃油税”“公务员加薪”等贴的讨论中，谁是“公仆”，谁是“主人”在普通老百姓的心中越辨越明，理性的精神，深遂的思考比比皆是，从“奴性、屁民”到我们就是“不明真相、一小撮”，特别是奥运之前空前的“抵制法货”回归到理性的思考，我们的“人民”正在转化为“公民”，中国是有希望的，无非现在是黎明前的黑暗而已。<br />
&nbsp; &nbsp; 鲁迅这个曾经被神化和革命化的伟大的思想家，我们应该还他人间本来面目，鲁迅是具有独立思考的文学家，他永不向权贵和政府妥协的精神，特别是他对中国“国民性”的批判，对中国专制主义的批判是我们思想“启蒙”的现代先河，只有从启蒙、教化才能回到理性和法制，最终才有民主和自由，空喊口号和大发牢骚、悲观失望是没用的，暴力和革命只能是新的改朝换代，代价更是巨大的。<br />
&nbsp; &nbsp; 田刚先生给我们讲述了一个真实的鲁迅，任何试图将思想丰富而又自身矛盾、堂堂正正的鲁迅贴上标签归入己内的企图都是失败的，鲁迅虽然不是自由主义者，但他是自由的知识分子。<br />
&nbsp; &nbsp;&nbsp;&nbsp;每月第一周六下午的讲堂期待更多的独立思想者来参与，来自由讨论与公开出版物相关的主题，启蒙部落期待更多的象狄马、田刚等独立思考者的支持和参与。<br />
