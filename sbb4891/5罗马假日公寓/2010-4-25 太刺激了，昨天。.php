																											<div id="postmessage_582236" class="t_msgfont">一直惦记着天北路的桃林。拖了3星期，昨天天气不错，破事也不少，拖到中午才出发，把公路和折叠装好，把车停在罗马湖边上，和领导环湖一周后沿着天北路一直往北。<br />
<br />
平路顺风，领导很轻松就骑到26的AVS，还非常得意说骑车一点也不累呀。出于对领导在骑车这事上还有巨大的发展潜力角度考虑，我没有透露现在是顺风的事实，而且是大约4级风。<br />
<br />
非常轻松的骑到北石槽，领导一直在看码表，“怎么这么快就到这儿了”一类的话说了4-5 次，作为下属，聪明的笑而不语。<br />
<br />
杯具的是08年看到的连绵桃林今年似乎都改种别的了，只有寥寥几处，领导兴致受到很大的影响。在北石槽的超市买了瓶水后就返程。<br />
<br />
虽然阳光灿烂，春光明媚，但逆风的事实让领导很难接受骑车居然是这么件无聊的事。咬牙跟了1-2公里后，一直是 17-18的速度前进。我向领导献媚说去太阳村的草坪上晒会太阳，我骑到罗马湖再开车过来接你。领导一开始为了维护自己的权威拒绝了这个动议。大约又骑了几百米，我再次动议，领导从善如流的接受了这个建议。<br />
<br />
一个人开始快速返程，逆风确实很讨厌。只能保持25-26的速度，偶尔上到30+，几分钟后就泄气了。<br />
<br />
途中跟了一个硬壳载客三轮。太刺激了！低头一看码表都40+了，换到小飞还有踏空感，跟了2公里多，路况开始有点不好，而且今天没戴头盔，万一摔车，半条小命就没了。赶紧解除跟车，反正也快到了。<br />
<br />
开车去太阳村接领导，一路遇上7拨人马。我刚才骑回来咋一个没遇上？NND。<br />
