　　监制的话 <br />
　　 我和无国界医生结缘于他们协助我揣摩一个“在前线工作的医生”的角色。他们提议我探访组织在埃塞俄比亚的疟疾治疗及预防项目。自始，只要时间许可，我便会和他们合作。 <br />
　　 就在那时，我产生了要替那些“不被看见的人”说说话的念头。他们就是那些饱受战火蹂躏或遭受鲜为人知的疾病缠身，却未被传媒、各国政府和药厂注视的一群。 <br />
　　 逐渐地，无国界医生希望大众关注何种危机的概念开始成形。初部构思是这个计划能让公众观看，唯一目的是提升公众对被遗忘的危机的认知。拍摄的内容是按无国界医生每年发表的“十大人道危机”来挑选。这十大人道危机都是一些急需人道援助的问题。 <br />
　　 感谢费南多・阿拉诺和马力阿诺・巴罗索等朋友的协助和意见，我才能聚合到五位天才横溢的电影制作人执导这出电影。有些导演我并不十分熟悉，如查维尔2柯库耶拉，而伊莎贝拉・科榭和云・温达斯更是首次碰头。然而，他们由始至今都有着共同的特质：愿意承担、勇气可嘉和宽大。 <br />
　　 此外，能让这几位举足轻重的电影导演抽空合作简直是一个奇迹，他们都赋予这部电影心力和灵魂。 <br />
　　 我想，我们对这些危机全然不觉的原因有很多，最主要的是真确信息不足，以及对社会上弱势社群的绝望困境漠不关心。我们愈来愈趋向以独立个体看待自己，而忽略了我们是以一个社群或一个团体而生存。戏中的人和你我毫无分别。他们是在残酷而不道德的经济制度下被剥削的人，以及各国政府对不能从中获益的战争采取视而不见的策略的受害者。这就是我的看法。 <br />
　　 给他们一些注视吧，不只是在医疗的层面上，而是给他们一个出路。非牟利组织如无国界医生努力不懈地减轻这些被遗忘的危机所带来恶果，又公开谴责导致这些危机的原因。我相信一个普通人要贡献社会的一个好方法，就是要确保更多人得知这个讯息，尤其是那些对缺乏经济利益的人采取置若罔闻的态度的人。 <br />
　　 ――查维尔・巴顿 <br />
