													<h2>硬座车厢生存指南</h2>
						
						
																											<div id="postmessage_469231" class="t_msgfont">（本科毕业的时候写的，原来发在豆瓣南周组）<br />
<br />
&nbsp; &nbsp; 毕业论文写完了，咱写点服务大众的。本人家在西南，在西北上学，大学四年每当寒暑假必独自乘火车往返家与学校之间，目前累计行程超过50，000公里。因家中贫寒，坐火车也只能坐硬座车；而本人身高不足150cm体重刚好40kg，以如此身材应付各种拥挤的环境却不曾伤到一个手指甲，不能不说是一大成就。在来来往往中本人积累了丰富的经验，这也算是一笔财富。此文写给因任何原因有需要或不得不乘硬座车旅行的朋友，按内容分列条目，供大家坐车时参考。<br />
<br />
&nbsp; &nbsp; 一、上车。本来是没什么好说的，这里说说连站台票都没有的情况下（赶不上买/买不到）怎样混到车上去。其一，等。一般来说始发车总是到了快开车的时候还是有很多人没检票（莫非全是想用这招的？），到了最后检票员根本连票都不看就往里推人了。其二，挤。估计身体弱的人都不好操作，这里只推荐给身强力壮的。有时候因为时间紧张，即使人人手中都有票也来不及掏出来就往上挤了，那么没有票的你就顺水挤上去吧！注意身边有没有小孩。其三，求。找个面貌和善的检票员，跟她/他说明你是因为赶不上才没买票的，请允许你上去再补票。这个行不行得通要看人，所以时间还够的话多求几个人，总会有人让你上去了。其四，爬，爬窗是也。需要内应帮你打开窗户，身手够灵活的就直接钻进去吧。<br />
&nbsp; &nbsp; 友情提示：上了车记得到十三号车厢办公席去补票。<br />
&nbsp;&nbsp;（闲话两句。上车时我最不喜欢的事就是，所有人在月台上都喜欢跑步前进。哪怕是在始发站，明明离开车时间还有半个小时，刚刚检完票的人到了月台上最慢的也是大步走，最快的简直就是在狂奔！从来没见过有谁是从从容容地走到自己的车厢跟前的，搞得所有人都慌慌张张。又不是上了车再抢座位，究竟他们在赶什么？）<br />
<br />
&nbsp; &nbsp; 二，食。短途旅行随便带点零食和水就够了。人多的时候最好什么都不要带。但是长途旅行的话就非带点吃的喝的不可了。人在坐火车时很容易便秘，所以原则上以按各人体质不同，选自己最易消化的食物为上。像番石榴之类的水果还是不要吃的好，除非你正好腹泻。如果你还愿意为别人着想，那么把榴莲留到终点以后。各人的食物最好上车前就准备好，火车上的盒饭没事还是不要吃了，理由是不卫生。乘务员卖盒饭的时候可不是把每份饭菜都装好了才推车过来卖的，配饭的菜都是装在没有盖子的大铁盒里，你要买，乘务员就给你打一份。想想那个菜在推到你跟前之前就暴露在各节车厢的空气中不知多久了……当然车上的盒饭也不是绝对不能吃。你如果实在想吃，那就等等吧。一盒饭刚推过来的时候卖十块钱，到后来最后一次推过来的时候就会变成五块钱了，忍他半个小时省一半的钱还是很划算的。<br />
&nbsp; &nbsp; 至于水嘛，人少的时候带个空水杯就好，毕竟水很沉。人多的时候自己买几瓶矿泉水纯净水什么的，尽量避免带饮料，因为饮料不如清水解渴，而造成的想上厕所的程度是相同的。<br />
&nbsp; &nbsp; 到了非常时期（比如你已经知道前方路上交通中断什么的），尽量多带食物和水。我今年年初回家时就被雪灾困在路上，亏了我多准备了两天的食物，才没被饿死在湖南……<br />
<br />
&nbsp; &nbsp; 三、睡。长途旅行，夜间乘车的最大问题就是怎么睡。不睡是不可能的――我曾试过带了一堆罐装咖啡上车，夜里开始困了喝一罐，又困了再来一罐……才喝下第二罐就睡着了。问题是除非车上人极少，否则没人能睡得舒服。当你坐着睡，头歪到一边，没多久就脖子痛。这里有一个最简单的方法：晚上睡觉前找件薄毛衣、厚衬衫或者厚围巾之类的衣物出来――注意，不能太薄，也不能太厚，以将其整个卷起来时能填满你的脖子和椅背之间的孔隙为宜。现在你能猜到这些衣物的用处了：把它们卷起来，垫在你脑勺后面。不要小看这点小小的支撑物，它能让你像个人样地坐着睡着而不会歪倒，而且脖子的酸痛也可免了！坐在靠窗的座位可以趴在小桌上睡，但是因为小桌最外缘的铁皮是突起的，会勒得胳膊很疼，解决的办法就是先拿厚一点的报纸或杂志之类垫上，再趴在上面睡。另外，如果你正好坐在一排三个座位最外面的那个，够不到小桌子而想趴着睡，还有一种可能，那就是如果你随行带了只大箱子，可以把它竖起来立在你座位旁，再趴在上面――反正夜里不会有乘务员推小车来卖东西了。不管你怎么睡都好，一定到注意不要把贵重的东西方在小偷容易偷到的地方，例如裤袋。<br />
<br />
&nbsp; &nbsp; 四、拉，撒。就是怎么上厕所。人少的时候没问题，人多的时候很严重。当车上挤到没座位的人只能站半只脚，哪怕厕所就在你跟前你也去不了。如果你身体还没强壮到能凭自己的力量挤过去，那就搭便车吧。推小车卖零食、盒饭、土特产……的乘务员是永远都勇往直前的，看到她/他们过来了，不管你现在急不急，都先跟过去吧，反正就算你当时不急，等你跟着小车终于“走”到厕所门口再排个队，也该是时候了。方便完了怎么回去？等那个小车再推回来呗！“搭便车”的原则也用于去洗漱、打开水……最后，长途火车上的厕所条件跟地上最脏的厕所差不多，无论你看到如何壮烈的情景都要镇定，解决问题才是关键！记得自己带手纸！！<br />
<br />
&nbsp; &nbsp; 五、洗漱。这在拥挤的火车上几乎成了最不可能的事，因为当你万里跋涉终于拎着口杯和毛巾抵达洗漱间，你将看到的是里面已经被占领且安然端坐在洗手池上面的人还在低头沉睡！但是爱干净的朋友也不用绝望，我们的原则是：有条件的要洗，没条件创造条件也要洗！首先要解决的是水的问题。洗手池的水龙头能出水当然就用它，等到你听说那里没水了――放心，此类消息总是传得最快的――就要开始准备了。纯净水的空瓶子不要扔掉，晚上睡觉前用它们去开水炉那里灌满水（小心烫！），拿回来放在安全点的地方，让里面的水自然冷却。第二天早上就可以用凉开水刷牙擦擦脸了。洗漱间被人占领了怎么办？到厕所里洗去。问题是：如果连开水都没有了呢？这就需要更早的准备了。你上火车之前总要上超市买点东西吧？顺便带一包消毒湿巾吧。实在没水洗脸的时候可以用消毒湿巾仔细地擦一遍。如果你嫌在座位上擦脸不雅观，可以假装擦汗慢慢来。没办法刷牙，老办法，嚼口香糖。无论男士女士，坐车记得带梳子。将自己的面容五官清理一遍，你会觉得自己比大多数人看起来都更像个人。<br />
<br />
&nbsp; &nbsp; 六、对付噪声。这一项似乎看起来不重要，其实不然。我坐这么多次火车，最生不如死的时候不是站在人群中几个小时不吃不喝动弹不得的时候，而是在睡梦中恍恍惚惚听到火车的广播里一直在重复播放《两只蝴蝶》们的时候。从那以后我每次上车都会记得多带一样东西：静音耳塞。这玩意儿一般书店里都会有卖，几块钱一副，相当划算。本来是为看书时抵挡噪音设计，所以效果良好。乘火车时把耳塞一塞，虽然不至于让你变成聋子，但是划拳猜码、小姑娘们咬着耳朵小声说大声笑、小儿夜啼、小青年们肆无忌惮地高唱情歌……其分贝可降低至你可以忍受的程度。还不行的话就自己带个mp3听音乐好了，当然最好不要在听《老婆老婆我爱你》得时候还用最大音量外放！<br />
<br />
&nbsp; &nbsp; 硬座车厢生存指南（番外篇）<br />
&nbsp; &nbsp; 以上的各种经验通用于硬座车厢内有座的和没座的。但是我在这里要把“无座”单独列出来，毕竟这是一个综合的性的难题。（顺带帮欧阳峰问一件事：有座和没座处境相差很远，为什么车票还是一样的价钱？）无论出于什么样的原因，总之你买到一张无座票，上了车，和另外一群同样没座位的人挤在过道里、洗漱间、车门边的吸烟间、车厢连接处（咱专业点儿）……行李扔在脚边（收拾收拾吧，乘务员过来你该挨骂了），装作无所谓的样子看外面或者和开水炉上的指示灯大眼瞪小眼，心里却在考虑接下来怎么办。事实上你不是没什么事可做的。因为车站卖无座票通常都先卖十三、十四号车厢的，所以这两个车厢里站的人能把你挤扁！所以如果你刚好上了十三、十四号，等车开动了以后就转移到后面的车厢去。自己挤不过去就“搭便车”。最好到最末一节车厢去，如果你够运，说不定能找到预留座位先坐坐。<br />
&nbsp; &nbsp; 当然一直站着是非常累的（过个十五分钟你就知道了），所以，没坐票的时候也坚决不能站着，要创造条件坐下来！以下提供一些建议以供参考。<br />
&nbsp; &nbsp; 蹭座。先主动跟人家搭话（注意不要太露骨），聊得差不多了人家多半会让出一点点地方来让你把半个屁股放上去。这时候就一定要死守阵地雷打不动！多跟人家沟通，到最后即使嫌多了个人挤得半死但大都不会好意思公开赶你走。<br />
&nbsp; &nbsp; 自己带报纸坐地上。《南方都市报》最好，一份一大摞，干什么都够了。把报纸铺在哪里是有讲究的，过道里就不行：每次有人经过你都得站起来。车厢连接处不安全。车门旁边到站开门你就得离开。理想的地点是当你铺上报纸坐下以后还能留出足够的空间让行人和小车经过。本人经过考察，认为车厢一头靠近乘务员休息室那个地方最好。那里靠车门的一排只有四个座位，空出来的一点地方也够你安营扎寨了。所以当你不打算蹭座时，一到车上赶紧把那个角落占住。<br />
&nbsp; &nbsp; 洗漱间和垃圾桶上面不推荐。尽量避免坐在开水炉前面。<br />
&nbsp; &nbsp; 自己的行李够结实，坐在上面最好不过（哭一声被我坐坏了的箱子）。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 有一种办法我自己没试过，但亲眼看见别人亲身示范，我想应该也可以写出来：睡到三人座位底下。进去的具体动作解析为：先在地上垫上报纸或麻袋，侧身躺在上面，两手攀住两边的椅腿，上半身连报纸或麻袋一起挪到地下；脚下再垫上同样的东西，然后整个人挪进去。我遇见的那个年轻人似乎十分精于此道，但见他进进出出，动作没有任何延滞，实在令人叹为观止。</div>

							
							
							
							
