													<h2>Amazon（us）一点购买经验</h2>
						
						
																											<div id="postmessage_628881" class="t_msgfont">A&nbsp;&nbsp;首先要准备一张双币种的信用卡（visa，master都行）<br />
B&nbsp;&nbsp;然后去Amazon注册一个账户（邮箱随意，境内外都行，我用的是gmail，手机填正在用的就行）<br />
&nbsp; &nbsp; 这里要注意写地址的时候不要有语义错误，例如XX&nbsp; &nbsp;street&nbsp; &nbsp;XX&nbsp;&nbsp;university&nbsp;&nbsp;XX&nbsp;&nbsp;college XX&nbsp;&nbsp;room&nbsp;&nbsp;tianjin&nbsp;&nbsp;china 300000（小到大，邮编国际通用）<br />
C&nbsp;&nbsp;挑选商品，确认购买（使用信用卡方式付款），在境外付款时visa是不需要密码的，在发出order之后大概一两天在你的信用卡“美元账户”上就有了明细<br />
&nbsp; &nbsp; 关于信用卡，如果不愿用美元还款的话，可以选择“购汇还款”<br />
D&nbsp;&nbsp;之后，在注册时使用的邮箱中会得到一个确认order的邮件，不用理他，之后可以根据他给你的链接查看商品的物流情况<br />
E&nbsp;&nbsp;最后，取国际邮件的地方是邮局（和国内邮政快递包裹的流程一样），但是有些地方的邮局工作人员可能英语水平欠佳，可能找不到包裹上的你的电话（国际邮件包裹上是纯英文标注），所以如果一段时间没收到可以去邮局确认一下，如果逾期但邮局没有给你电话的话，也可能会要求你支付滞纳金，但这种情况其实是不需要支付的（我遇到过一次这种情况，具体怎么说你懂的）<br />
F&nbsp;&nbsp;最后也不需要特意去确认收货，Amazon是以邮局是否发到为标准结单的<br />
G&nbsp;&nbsp;关于什么东西可以邮购什么东西不允许，我并不能给出一个完整的名目，但是，根据我的经验，书籍，食品，日用品，小电器都可以顺利送达并且无需关税，其他我没买过，也没有认识的人买过，所以恕难详答<br />
