													<h2>喝茶记</h2>
						
						
																											<div id="postmessage_687842" class="t_msgfont">上次喝茶还是去年7月份，湖北警方不辞辛苦从大老远跑来广州请“喝茶”。那次是因为网站的事情，而这次是因为六四文化衫的问题。<br />
<br />
据公司行政人事部门同事说，广州市国保部门在上月底开始就在公司调查本人的资料了。由于前两日我不在公司，所以未能成行。而6月2日下午，我在公司，他们来了。<br />
<br />
本来约好2日下午2点半会面的，但他们迟到了将近20分钟，面谈于2点50分左右才开始。<br />
<br />
我见面就说，你们终于来了！？我没有向他们要证件，他们也没有向我查证件。对方有两人，一个稍矮的姓陈的警官负责问话，稍高警官的负责记录。于是谈话就开始了。<br />
<br />
谈话以我的简历开始。他们前日已在我公司拿到了我的简历。他事先以公司大、工作和待遇不错来威胁我，似乎有随时可以把我从公司搞走的意思，结果我直接说我就是上当才来这家公司的，因为这公司不兑现他们的诺言。他们一时无语。<br />
<br />
他们说是因为我在推特上公开我穿“天安门母亲”文化衫一事来找我的。要没收我的文化衫。我回答可以，但是能否给件衣服我换？对方说没有！<br />
<br />
以下是6月2日下午和6月3日上午共计约3小时的部份对话回忆。<br />
<br />
关于政治:<br />
<br />
“爱好中写着喜欢运动、摄影、音乐，为什么没写政治啊？”陈警官翻翻我的简历问。<br />
<br />
我回答道：“因为政治是一切社会生活的构成基础啊，什么都离不开政治。关心这个有什么奇怪？”<br />
<br />
关于六四:<br />
<br />
陈警官说:“关于六四的问题，中央早就定性了。不能说谁对谁错的问题，你从中央的态度也可以看到，那是一场政治风波。所以就不必争论了！”<br />
<br />
这个问题我觉得太白痴，我没有回答，只是笑笑表示回应。我以消极的态度来和他们对抗。这个问题白痴在于杀人的凶手是要得到法律的惩处的，该赔偿的要赔偿，不是你中央说了不争论就不争论，你至少要说出来让人们了解真相，否则那就是做了亏心事不敢当。而且历史预示着未来，让大家搞清楚历史，在历史中总结教训和经验，避免悲剧不再从演。对此类问题我发表的观点已太多了，我懒得继续争论。<br />
<br />
关于中国经济问题：<br />
<br />
陈警官说：“你看，从80年代改革开放以来，我们取得了不错的成绩嘛！别总是盯着那些不好的东西。”<br />
<br />
好在我对中国经济有些自己的意见：“我不否认有成绩，但如今你看到的只是表面的光鲜，这经济是靠什么换来的？巨量的环境污染、低人权低福利、高能耗等等，你看走到现在的结果是什么？”<br />
<br />
陈警官说到现在政府也在提高最低工资待遇之类的，说明在解决这些贫富悬殊的问题，我见他一点经济学常识都没有，我也懒得说最低工资制度有可能推高失业率的问题了。所以我没有回话。<br />
<br />
关于地震：<br />
陈警官说：地震的东西有什么好炒的？官方都说了没问题！没公布名单又说没公布，公布了人们又不相信。<br />
<br />
我说：“没问题？那些记录片、照片中明明看得到建筑质量问题。再说，追究豆付渣工程有什么错？设身处地想想如果你我的孩子因这样的工程质量而失去了生命，你将如何想？”<br />
<br />
关于理性和言论自由：<br />
<br />
陈警官说：“我劝你最好少关心这些事情，少和那些异议份子接触！”<br />
我说：“我很自私，我关心这些完全是想我以后的小孩不要被喝毒奶粉、不要被打毒疫苗！我没有那么伟大，我只想我的小孩以后自由生活在一个健康的环境中，我相信你也有一样的想法，但是我在做。我没杨佳那副身板！不这样你叫我哪样？”<br />
<br />
说到关于接触异议人士，我说：“任何思想都要先交流，经过自己的推理才能得出对或者不对。我不能光听官方报道如何，我得去了解他们的真实想法，去与他们沟通，我才能知道他们是所谓的好的还是坏的，光听宣传哪行？比如这两天我们这样，不沟通，你可能觉得我是个激进份子，但经过交谈，我相信你对我是另外一番看法。”<br />
<br />
我还提到言论自由和理性的问题，我说：“光听官方单一口径的宣传，而没有多元化的对比信息，民众容易失去准确的判断力，很容易引起偏见，暴力。多元的资讯让人们思考、对比，而理性的人应该在言论自由的环境中培养出来。”<br />
<br />
<br />
关于零八宪章：<br />
<br />
陈警官说：“零八宪章你签名了吧？！”我说是！<br />
<br />
我估计是他请签名者喝茶喝得多，所以他补充：“其实零八宪章就是宪法的一个翻版，这个我就不追究了！”<br />
<br />
我说：“零八宪章是一个理性的建议，也是若干年前共产党曾经提出过的东西，这个还有什么好说的？”<br />
<br />
后来他们把我的六四文化衫拿走了，说不希望在网上看到我说起这些事情。我则在推特上回应了他们的话，我说我暂时的妥协并不代表我承认你们的做法是对的！<br />
<br />
我认为，喝茶并不代表双方一定要对抗，应该是互相理性交流，不怕要抱敌视的态度。当然你不能否认对方可能会利用公权利耍流氓，我倒是希望流氓学会讲道理。如果官方学会和民间异议人士尝试理性对话，我倒是持欢迎态度！<br />
<br />
对我进行威胁，是没用的，特别是说不在同事面前把我带走是因为为我好之类的，这本来就是可笑的说辞，因为我没干违法的事儿。我倒是希望他们告诉我所有的同事，我究竟干了些什么才会这样。<br />
<br />
以上的我记忆的一部份主要内容，是为肖某人喝茶记！<br />
