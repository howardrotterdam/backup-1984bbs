&nbsp; &nbsp; 到北京的地铁，就仿佛进入了文明，上海的地铁设施大多是新建的，就像装修好的新家，进去总感觉怪怪的，没有人气儿，少了主人和它的磨合，这就没有感情，只有物质，东西是东西，人是人，没有意思。反倒是北京地铁好，不论是安检大爷还是姑娘，透露着情调，没有厌烦，没有敌视，京片子说：包儿，包儿，包儿，都放这儿啊……<br />
下包过案件，一北京阿姨官员模样走过来对两位安检姐姐说：等会两位区长要来啊！都小心着点儿！ 虽然区长来不来无所谓，但看到京城大妈狗腿的神气儿，我也顿时来了精神，帝都，大爷我又来了……<br />
&nbsp; &nbsp; 电话有问题，电池也跟着毛病，我这儿电池，一天就得一换，临行走的匆忙，电也没充满，看来得省着点用，临行前一天约了群里一位少爷，“要”请中午麦当劳，少爷在玉泉路，我记得是一号线，途中一直发短信未回，走到木樨地，短信来了：刚起，对不起，有事。&nbsp;&nbsp;操！出师不利，我到那都怕给别人添麻烦，花大了别人钱更不好，一个薯条，两个圆筒――这就是我的心理诉求。可能少爷怕我赖家里不走，或许是真有事，罢了，北京我还是很熟的，转行换乘北大去，途中远在新疆的一位大爷打来电话，静音未接，回拨，大爷在乌鲁木齐的小旅馆里搂着黄毛姑娘对我表示因出差加时不能及时回京以至于说好请吾脱鞋在星巴克喝咖啡的愿望不能实现而导致的慰问，两句话，赶紧挂断，操，我拿着一个上海带来的卡在北京往新疆打电话，我胆儿真肥……<br />
&nbsp; &nbsp; 北京的四号线和十号线都通了，刚来北京的时候简单的地铁图顿时复杂起来，那时只有四条线，天天从天通苑往城里跑，去蓝旗营得先五道口，我还是挺得着从五道口往北大走的一路呢，舒坦，文化感。直接到北大，记得去年今日，我尚有高中学生证在手，信心满满，对比旁边的游人，不但精英感十足，合法性也十足，拿出身份证和学生证一甩，问：来干什么？答：找人！问：去哪？答：哲学系！（估计要是答湖就被清退了）虽然现在我已经是正宗的社会资深盲流无业游民了，但在上海，别的没学会，腔调还是学到了其精华的，昂周阔步，两眼目视前方，径直进入，成功！<br />
