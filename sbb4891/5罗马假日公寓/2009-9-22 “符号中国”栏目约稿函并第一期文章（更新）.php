																											<div id="postmessage_324342" class="t_msgfont">蒋蓝：豺及其发声术<br />
<br />
<br />
四周所有的豺狗，还有在这期间从远处又来了许多豺狗，它们把头夹在前腿中间，并用爪子搔着头；它们好像在隐瞒着它们的厌恶，这种厌恶太可怕了，以至于我恨不能纵身跳出它们的包围圈……所有的豺狗跟着嚎叫起来。这时，远处好像有人在唱歌。<br />
―卡夫卡《豺与阿拉伯人》<br />
<br />
豺是象形字。李时珍认为：“豺能胜其类，又知祭兽，可谓才矣。”当然了，豺也通“柴”，俗名“体瘦如豺”是矣。这从内外两个向度上，清楚勾勒了豺的双重形象：聪明，瘦削。<br />
这种特征比狼、狐狸要显著得多。因为狼偶尔喜欢独行，有一种孤独的清冷和冒险意味，它们过于执著，往往显得不知进退；狐狸则过于小心，是“慎独”的模范，它们总是在预感的边缘徘徊，展开自己美丽的拖地长裙，似乎不是为了食物，而仅仅是炫耀发亮的身体。豺没有这些形而上的品味，豺是实用主义的信徒，它们吵吵闹闹，一拥而上，全无风度。<br />
亚洲的豺狗（Cuon alpinus，也称豺，亚洲野犬），它们体型虽然小于狼，但是凶猛程度可能更甚。豺是典型的山地动物，好群居，善于围猎，是集体主义始终不渝的实践者。有一个报道曾经指出，单只豺狗的实力不如鬣狗，但豺群的规模和韧性远大于鬣狗群，不同群的豺狗甚至可以联合起来一起对付猛虎，曾经发生过先后近60余只豺狗以惨重代价咬死一只雌性孟加拉虎的著名战例，就是不同的3群豺狗组成的。<br />
而豺的欺骗手段并不亚于狐狸。豺狗叨走鸡后，总要把它埋藏起来，以躲避失主的寻找和猎人的追赶，事后再挖出来慢慢享用。所谓聪明，在猎食一道自然就演变为一种残忍技术，围猎时，它们首先把猎物的眼睛抓瞎，然后设法予以消灭。一般的狗类爪子并不如刀，但豺亮出它们的指爪时，我们就发现这不但是一把利刃，而且还带了倒刺。一旦发现猎物，其中一头豺狗就会连吓带哄尽量拖住猎物，不让猎物胜利逃亡，而其他的豺狗就从两侧快速包抄，堵住逃路。这时候，猎物进退两难，靠近其尾部的豺狗就会乘机跳上猎物的背部，然后用利爪掏出猎物的肠子，当猎物负痛亡命狂奔时，被掏出来的肠子会夹挂在树枝上，肚空血尽而毙命时，豺狗便一涌而上，抢拖撕咬，将猎物吃得干干净净。<br />
能够体现“豺智”的场面是它们搏杀体格威猛的牛。一只豺狗会跑到牛的面前嬉戏，另一只豺狗则跳到牛背上用前爪在牛屁股上抓痒。当牛感到无比舒服而翘起尾巴时，豺狗就会对准牛的肛门痛下杀手。这种“黑虎摘桃”的独门武功是它们智慧的显形：在最薄弱的阴私地带打击敌手。这固然十分奏效，但就阴鸷而黑暗了。<br />
豺总是如鬼魅突然出现于文明的视野，这种阴风一般的造访使人们猝不及防。按照民间说法，豺是猎神的狗（一种说法是二郎神的猎狗，二郎神即是猎神），所以在食肉猛兽中，豺最威猛。豺狼虎豹，豺名列第一！有豺出没的地方，狼、虎、豹一定回避。豺是惟一一种将动物骨头咬碎吃掉的食肉者，据说整个动物界也只有豺狗才有这样咬碎骨头的能力。一些猎人认为，所以什么野物都可以打，就是不能打豺，一打必然遭报应。在雪域地区，宗教人士认为马熊、狼、豺三种都是魔的牲畜，猎杀它们必将激怒恶魔，魔损害人的心脏，杀生者的子孙七代都上了魔的黑名单，罹患不可救药的疯病而死，有些因脑溢血而亡，有些因心情悲伤而自尽，有些则是骨肉相残，血腥屠杀而毙命。他们后世要偿五百次生命，最终还要堕入众合地狱，人间17亿年中不得解脱。<br />
在《神曲》里，诗人但丁走在神示的道路上，突然在一片黑森林中迷路了。于是，在他的面前出现了三头守护黑暗的猛兽―豺，狮，狼。这固然是魔鬼的使者挡道，但我以为是自己心魔的造像，与狮子的刚猛、狼的如影随形不同，豺显然是阴险、毒辣的镜像，诗人必须打破迷障。他渴望引领，渴望圣女贝阿特丽齐的引领，退去猛兽，步出绝境。<br />
反过来看，在豺群频繁出没的埃及，豺获得的神话地位则高于汉语当中的地位，不是由于埃及人对豺的恐惧，而是他们看重豺的另类品行。希腊历史学家希罗多德在公元前5世纪时，曾经目击过埃及人制作木乃伊的过程。他说，为首的防腐匠人头戴豺的面具。这可能是因为豺具有“清洁大地”的本事，因而后来才又出现了头如豺、专门接待死人的阿努比斯神。<br />
　　豺在夜间喜欢发出嚎叫。那是一种极其奇怪的声音，像一群迷路的探险者在绝境中号啕大哭，毫无猛兽叫嚣的威猛与不可一世，也没有孤独的幽明，它们嘶哑而又尖利，绵绵不绝，震动山坳，像一把逐步张开的弯刀，弧度弹直，把单刃开口为双刃，刀柄融化在黑暗里，一并成为锋芒的身体，把夜色剖为无数的落叶与木屑，石头被翻转，将躲藏在石头内部的忧伤全部放逐到旷野。人们听着听着，就会莫名其妙地流下泪水。<br />
豺群仰天长嗥的意义，使狼嗥孤独得到了彰显。孤独的狼嗥犹如“大漠孤烟直”，使一些另类的人喜欢上了狼。豺声是缺乏这些意象的，它们的发声使得那些准备上演的崇高庄重的悲剧失去了施展的必备条件：价值遭致毁灭或失落，以往富有悲剧色彩的仰天长嚎，长歌当哭，在豺群莹色的注视下被置换为长嚎当哭。这让听众无所适从：既无法喜欢，也无法厌倦，豺声飘在听觉深处，仿佛把一条灵魂的光带打开，然后又疯狂地卷裹起来，蹂躏，揉成一团铁皮和玻璃，直到灵魂彻底被声音剁烂，成为一地的碎渣，使听众心智失控，既无法摆脱，也无从关闭……<br />
很显然，一阵又一阵刮铁锅似的豺声比绝望之歌更为暧昧，也更加凶险，它们企图使所有的听众失去定力，就像面对一个巨物，只用一根针来给它放气。<br />
　&nbsp;&nbsp;当豺群消失在野史当中，豺声却如骨头一样立在汉字当中，成为庙堂里发出的愤怒之声，声震屋瓦，粉墙摇曳。<br />
&nbsp; &nbsp; 人的命运似乎都是冥冥上苍决定了的，所以《易经》里说：“乾道变化，各正性命。”相术也是一样，起源于先民，到春秋战国时已有很多记载。比如周室内史叔服、楚之令尹子上、越国范蠡、秦之尉缭子都会相术。叔服说公孙敖的儿子谷―“丰下”（下颌丰满），“必有后于鲁国”；子上说太子商臣“蜂目而豺声，忍人（残忍之人）也”；范蠡也曾经说“越王为人长颈鸟喙”，故离他而去；尉缭子说秦始皇“蜂准（高鼻梁），长目，鸷鸟膺（胸部前凸），豺声，少恩而虎狼心”，遂离去。如此之多的拟动物化的形象描绘，最为出色的还是“豺声”。不单是秦始皇有，那个叫商臣的太子也有，他们其实是“豺声”的杰出代表。这不但暗示了这些王者的面目，关键的是还突出了他们心地的声音征象：阴暗，嫉恨，凶暴，以及颇为混乱的交媾史。言为心声，我们可以想像在“豺声”统治下的子民是怎样的境况了。<br />
《左传》宣公四年，楚司马子良生子越椒，“是子也，熊虎之状而豺狼之声；弗杀，必灭若敖氏矣”。而昭公二十八年记载，叔向的儿子伯石生下来时，叔向母亲听到伯石的哭声，说：“是豺狼之声也。狼子野心。非是，莫丧羊舌氏矣。”《国语&#8226;晋语八》对伯石亦有类似记载。这里，“豺狼之声”被用来形容一个人的声音特征，具备这样特征的人往往被视为不祥，这样的人最后都能毁家灭国。<br />
在后来的《世说新语》当中，也有近似的识人技术。潘阳仲见到年少的王敦曾说：“君蜂目已露，但豺声未振耳。必能食人，亦为人所食。”他预言了王敦的叛乱之心，必然招致身败名裂的下场。<br />
　　作为权力天然的发声术，“豺声”充分展示了权力的内幕与本质。它利用声音在空气里的震动，将声音背后的发声源烘托出来，使决绝的牙齿、反切的利爪、饕餮的胃囊逐一呈现，所有的猎物都将成为声音的营养和润滑剂。在豺声先行者的启发下，那些后来者于是进一步强化了其技术性，他们可以在“莺声燕语”的副调逼近中与你促膝谈心，并随时可以在“豹视”与“狼顾”的加盟下，“豺声”大振，恢复声音的本来面目。<br />
　　在高音喇叭的年代，在万人集会的广场上，我们经常能够听到一些激越的发言，庄严的声音由于被大旷野的冷风撕裂，残留在人们听觉里的无一不是豺声的翻版。<br />
　　世传狗为豺之舅，见狗辄跪。这个说法仅仅是体现一物降一物的思想，但我推测豺见了狗不但不会下跪，它们还会打开它们的金嗓子，让世界在自己的金属的碎裂声里弯曲而柔软，最后露出耻部……</div>

							
							
							
							
