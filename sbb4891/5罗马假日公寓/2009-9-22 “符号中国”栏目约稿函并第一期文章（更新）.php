																											<div id="postmessage_324338" class="t_msgfont">张闳：桥之梦：存在的焦虑与惆怅<br />
<br />
桥梁是建筑史上最伟大的发明之一。桥的存在，首先提示着阻隔的存在，但同时又意味着对阻隔的克服，意味着沟通和交流的实现。当初摩西如果拥有一座桥的话，那么跨越红海的壮举就会变得轻松自如。<br />
在桥梁出现之前，道路被水流所隔断，舟船承担了过渡的功能。舟船的漂浮性和摇摆性固然给航行带来了某种乐趣，但通过舟船航渡的危险性是显而易见的。而桥梁则通过其桥墩、桥台和基础，与大地联系在一起，它的稳定性给人以安全保障。海德格尔称赞道：“它使河流、河岸和大地相互成为邻里。”<br />
一般而言，这一特殊的建筑是道路的衍生物，它在道路的危机时刻出现，并使面临中断的道路得以延续。但其形态和功能均不同于一般的道路。桥完全独立于路，构成一个自足和完整的建筑形态。这个架空的人造通道，由包括桥身和桥面的上部结构与包括桥墩、桥台和基础的下部结构两部分组成。桥赋予路以飞翔的形态，使匍匐前进的路腾空而起，如同爬行动物向鸟类的进化和飞跃。作为路的变体，桥是对路的危机的拯救，并实现了路的梦想。<br />
然而，在文艺作品中，桥常常作为惊险情节展开的空间。桥出现在一个危机的节点上，一个空间连续性断裂的时刻。这个偏离存在之中心的地带，有着不确定的、未知的和偶然的特征。桥让两个彼此分离的空间偶然地连结在一起，这一特性与情欲的发生机制颇为接近，因而，桥的主题常常包含着有关情欲的暗示。桥常常成为邂逅或分离的场所。商业电影《廊桥遗梦》、《滑铁卢桥》（《魂断蓝桥》）和《新桥恋人》等，都表达过这一主题。<br />
在河流或沟壑横亘之处，桥梁超越其上，建立了一条由此及彼的通道。桥梁比道路更加明确地勾画出“彼”与“此”的界限。桥提示了彼岸的存在，诱发了对彼岸的梦想，并且，它可以满足彼岸梦想的实现。或者，至少可以说，桥是一个带有乌托邦色彩的中介物。正是居于此种理由，海德格尔才会赋予桥以深沉的存在论意义―“桥梁以自己的方式使大地和天空、神圣者和短暂者聚集于它自身。”<br />
但与海德格尔的乐观主义的哲学阐释有所不同，在现代主义艺术家笔下，桥的主题则常常显出另一番面貌。挪威画家蒙克即喜欢以桥为对象。在《不安》中，桥被表现为一个焦虑和梦魇的空间。画面上，人群如同幽灵一般从桥面上通过，表情一派迷惘和焦灼，远处有教堂的塔尖若隐若现，暮色四合，行人却不知将往何方。其情形宛若少年尼采所吟唱过的：“当钟声悠悠回响，我不禁悄悄思忖：我们全体都滚滚，奔向永恒的家乡……”相同的情形在《喊叫》、《忧郁》等作品中得到了强化。桥面上的孤独者，形象模糊、身份不明的人，无缘无故地喊叫，或者陷于无边的忧郁之中。蒙克笔下的世界，显露出黄昏景象。轮廓模糊的空间，传达出存在的不确定性和存在者无可名状的本体焦虑。桥，在这里似乎不再意味着连结。它将人群置于悬空状态。它是大地裂隙处的一个不稳定的黏连物，远离家园的一处令人不安的地带，一个无所依凭的场所。深渊之上，“神圣者”隐晦不明，“短暂者”无家可归。<br />
电影《卡桑德拉大桥》把桥所蕴含的危机意识推向了极端。这是一部灾难题材的商业电影，但其中所包含的生存焦虑经验，却暗示着现代人的深层精神危机。一列危机四伏的列车，不停顿地驶向不可知的未来，其终点将是一座深渊之上的危桥―卡桑德拉大桥。大桥并非通往幸福彼岸的通道，而是通往深渊的陷阱。尽管危机在最后一刻得以解除（列车在冲向危桥的一刹那，停住了，列车上的病毒威胁也消失了），但深刻的危机意识和浓重的宿命论色彩依然难以消除，从中也可以看到基督教“末世论”神学的微弱光芒。<br />
中国古典艺术中的桥却似乎更接近于海德格尔式的桥的想像。中国古典绘画中的桥很少表现生活世界的惊险情节，它甚至都没有任何情节性的因素。桥并不作为“此岸”―“彼岸”之间的相勾连的通道，它只是此岸世界的一片景色，是融入天地山水之整体中的一个细小片段，它总是与舟楫、草庐等事物一起，构成静谧孤寂的风景的一部分。诗人马致远的《天净沙&#8226;秋思》中描绘一幅有桥的风景：“枯藤老树昏鸦，小桥流水人家，古道西风瘦马，夕阳西下，断肠人在天涯。”尽管有因空间辽远而引发的“断肠”之愁，但不同于蒙克笔下的空间焦虑，桥在中国古典艺术家那里所指涉的乃是一种时间惆怅。桥依然与树木、流水、道路、房屋等一起，构成了可供暂时栖居的处所。水流与桥梁并不构成空间冲突和对抗，相反，它们只是表达一组貌似对抗的事物之间的差异和对话。水流模拟阻隔，并非真正的隔绝；桥梁也模拟连通。它们只是阻隔和连通的代码。而在小桥与流水的阻隔和连通的游戏中，给诗人及其他生存者带来的是某种情调、乐趣和意境。这种趣味在古典园林的桥梁艺术中达到了极致。园林在一个缩微的空间里，模仿外部世界的山水和道路，并尽量增加复杂和曲折，营造尽可能丰富、广袤的空间。而每一个景物都被高度符号化，成为外部世界之事物的某种拟像。以浓缩的空间来浓缩时间，以对想像中更为丰富的空间的占有，来完成对时间惆怅的克服和超越。<br />
也正因为这种空间的游戏性，即使是桥的断裂，也不成为空间危机的象征，相反，断桥的“残缺之美”，所带来的是一种更奇妙的美学效果。“驿外断桥边，寂寞开无主。已是黄昏独自愁，更著风和雨。”（陆游《卜算子&#8226;咏梅》）由于桥的断裂而造成的人迹罕至的结果，形成一种萧条和荒凉的意境，更能激发时间惆怅的情绪。因此，中国古典诗人和艺术家并不热衷于表达坚固的桥、不朽的桥和永恒的桥，相反，在所有材质的桥梁中，他们更钟情于木桥，即所谓“板桥”。板桥在古典中国文艺作品中享有更高的荣耀。木桥的简约和质朴的品质，符合古典中国的诗学理想。“鸡声茅店月，人迹板桥霜”（温庭筠《商山早行》）的情形，被视作一种超迈的意境。另一方面，板桥的脆弱性和易于显露的时间痕迹，也更适合扮演桥在中国空间诗学中的游戏性角色。<br />
现代中国艺术很少表现桥，偶有表现，往往是一个较为孤单的存在。习西洋油画的吴冠中、陈逸飞等人，一俟转向表达风景，他们笔下的桥也同中国古典绘画中的桥一样，只是整体风景的一部分。<br />
不过，革命美术在处理桥的主题的时候，试图赋予桥以某种深层含义，尽管革命美术也很少表现桥。古元的套色木刻《桥》，把桥的主题与革命战争联系在一起。革命战士用身体架起通向象征着暴力和胜利的桥梁。桥，在这里不再是一个物质化的建筑物，而是革命战争的辅助设备之一，是被革命者的身体所建构和铺垫起来的通途。《桥》改造了桥的一般意义，并传达了革命时代强烈的空间变革的诉求。这座人工浮桥所指向的目标，并非一般意义上的连结与沟通，相反，它所凸显的是被河流所隔开的对岸之间的政治和军事冲突，是此岸与彼岸之间暴力化的征服与反征服的斗争。对于桥本身来说，也反映了这一斗争哲学。人工的桥与自然的河流构成了尖锐的冲突。桥梁与水流之间，是一种克服与被克服的对抗关系。当然，这种冲突在古元那里，最终是以自然障碍被革命意志所克服而告结束。毫无疑问，随着一场战斗的结束，这座短暂的战斗性的桥梁的命运也将宣告结束，惟有革命美术依然顽强地以入木三分的方式，刻画着深刻的红色记忆。</div>

							
							
							
							
