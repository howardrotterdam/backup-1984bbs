微博客：神奇的“推特王国”<br />
<br />
《中华人民共和国宪法》规定：“中华人民共和国公民有言论的自由。”虽然人人都知道这是一张画饼，不过“推特（Twitter）现象”将这张画饼的虚假面具推到了极致。<br />
Twitter是鸟鸣声，借用鸟儿鸣叫的叽叽喳喳来定义这一新型的网络服务。按照权威的解释，Twitter是微博客的典型应用，“利用无线网络、有线网络和通信技术进行即时通讯”，“用户可以将自己的最新动态和想法以短信息的形式发送给手机和个性化网站群，而不仅仅是发送给个人”。因为每次只能发送140个字符，故称“微博客”。推特即Twitter的中文名。<br />
中国最有名的微博客是饭否，人称推特的中国山寨版。饭否于2007年5月创建，经历了一段自娱自乐的寂寞日子后，在2009年的春夏之交突然变得炙手可热，一大批自由主义知识分子的进驻和热议使得饭否终于在新疆7&#8226;5事件后的7月7日关闭。这段历史，所有的饭友都耳熟能详。紧接着，规模庞大的饭友们开始了规模庞大的流亡生涯，在涌入国内其他微博客（叽歪，做啥，嘀咕，滔滔，蟹爪，等等）的同时，同向地也涌入了推特。在翻墙技术普及化之后，推特更成为中国微博客用户的最佳栖息地。<br />
这就是神奇的“推特现象”，一段精神上的流亡旅途就此展开，伴随着对黄金岁月的饭否的无限缅怀。不同于异议者的肉体流亡，推特尤其是生活在国内的中国人的精神流亡地―当然，每一位推友都盼望着尽快结束这段茫茫的流亡生涯，怀着收复失地的快感回到墙内。如同必须暂住在自己的祖国一样，这些精神流亡者也必须暂住在墙外的虚拟空间，在这个虚拟空间里真实地发言，人人都像高段的太极推手，把蜂拥的信息、话痨者的呓语和言论自由的信念推来推去，渴望早日推出一个崭新的自由国度。<br />
这是一幅让人欢欣鼓舞同时又令人心酸的图景。流亡，这个远离人们日常生活的陌生词汇，在GFW（伟大的防火长城）的虎狼觊觎之下，终于把它“推”到了一部分先说起来的人的内心深处，成就了言论自由的“推特王国”。<br />
在这个心理背景之下，又映衬着饭否的流金岁月，目前任何一个墙内的微博客都会成为饭友和推友的调侃对象：蟹爪被在校的“小清新”们攻占，成为校内网的镜像；新浪微博客（昵称围脖）短短几天内测，因为审核、删帖和敏感词过滤，立刻就使人心生厌倦；雅虎微博客（昵称咪咪）英文版页面被中国用户频繁刷屏之后，变成了“快来摸我咪咪”的戏谑欢乐营……因此有推友断言：在早期的亢奋期过去之后，最终还是要回到推特。<br />
心怀自由的人们在“推特王国”流亡，当政者也在自己的“推特王国”疯狂。这个官场版的“推特王国”，是一个光怪陆离的荒谬道场，道场内的每个官员和办事员都不敢为任何事负责，从上到下，人人都如同推友一样也是高段的太极推手，只不过他们推来推去的是责任。对他们来说，“推”就是“卸”。迥异于Twitter的平等关系，这个“官场推特”有着壁垒森严的等级制，只能同级推或者往下推，最忌讳的是往上推。实在推不下去往上推的时候，就意味着有人的顶戴花翎要被拿下了，此一倒霉角色俗称替罪羊。<br />
两大“推特王国”其实是同一枚硬币的两面，二者的本质区别是：前者推来推去，推出的是意见领袖和公民行动者，终极目的是推出一个民主自由的中国；后者推来推去，推出的是一个或者若干个替罪羊―替罪羊，这个来自希伯来人的悲悯称谓就此污名化为极权的肮脏牺牲品。当每个人都被推成替罪羊和牺牲品的时候，它的末日也就来临了。</div>

							
							
							
							
