&nbsp; &nbsp; 清华美院雕塑系设计的56根“民族团结柱”，由水泥和钢结构构成，外包金红两色玻璃钢，其上雕有一对身穿节日盛装、载歌载舞的民族青年，背面则雕以该民族的金色纹章。据设计者宣称，其柱头和柱础均采用人民大会堂东侧门柱的图案元素，以体现56个民族是共和国主人，而柱子本身则用来象征56个民族的人民是国家脊梁。它们分列于天安门广场东西两侧，共同支撑其政治伟业。该柱头还安放了焰火发射器，并在10月1日晚上予以燃放，试图制造“生日蜡烛”的庆典效应。<br />
&nbsp; &nbsp; 民族柱在天安门广场的出现，意味着天安门建筑体系的重大推进。该体系历经三次历史剧变。第一次是它在1958年的初步成型，其核心建筑为人民英雄纪念碑，由此确立了其作为祭奠空间的功能主题；第二次是1977年毛泽东纪念堂的落成，强化了广场的祭奠含义；第三次就是2009年民族柱的设立，在灵堂和纪念碑两侧各竖起28根立柱，由此完成华夏陵园格局的最后营造。<br />
&nbsp; &nbsp; 在天安门广场的最新格局和传统宫式墓地之间，出现了高度同构的景象。西安乾陵是这方面的典型分析样本。在通往梁山墓冢的神道（司马道）两侧，至今还保存着华表、石碑、翼马、仗马、鸵鸟、石狮、10对翁仲和61尊宾客之像，而居首的就是一对8米高的八棱柱石华表，它是帝王陵墓的主要标志，作为男性生殖器造型，它旨在表达李唐王朝的血脉永存；诸宾雕像，则分别代表出席高宗葬礼的各地藩王与使臣。耐人寻味的是，在天安门广场，这些象征帝王生前仪卫的石人石马，恰好被代表56个民族的立柱所替代，它们褫夺了护陵者沉默的使命。这意味着为毛泽东或“人民英雄”守灵的，不再是传统的神人神兽体系，而是以56个民族命名的“13亿中国人民”。<br />
&nbsp; &nbsp; 以人民英雄纪念碑为空间核心，天安门广场早已获得亡灵祭拜的权能，而经过毛泽东灵堂和民族柱的两度添加，这项功能已经上升到无可置疑的高度。2009年10月1日，这座世界上最辽阔的广场，终于完成了作为国家祭坛的终极定义。这当然是无比正确的定义。在许多人看来，天安门广场就是全体中国公民的第一祭坛，用以缅怀20世纪初叶以来所有为民主、自由和科学奋勇献身的志士。<br />
&nbsp; &nbsp; 但本次出现的民族柱，并未继承天安门华表的质朴造型，也没有采用大会堂和纪念堂立柱的既定样式，而是向民众炫示一种奢靡华丽的风格。金色是财富的象征，红色则是权力的象征，夸张的金红二元色谱，制造了财富和权力的双重狂欢；此外，其上雕刻各种“歌舞”造型，也只能用以表达政治喜庆，却不能传递民众的哀思，更无力唤起对历史的理性反思。所有这些元素都跟祭奠空间的本义严重冲突。<br />
&nbsp; &nbsp; 这种“金红色谱”最先出现于佛教寺院，而后被张艺谋的影片《满城尽带黄金甲》所袭用，继而成为最新修缮的天安门城楼乃至故宫的主题色调，如今又爬上民族柱，成为装饰广场的艳俗道具。这是旧帝国及其遗老的趣味，它的滥用，已经制造了遍及中国的视觉灾难，而今又瓦解了中央祭祀空间的肃穆性，令整座广场散发出轻浮庸常的气味。<br />
&nbsp; &nbsp; 天安门广场的原初设计，未能解决宫式建筑（红色的天安门城楼）和苏式建筑（灰色的人民大会堂）之间的风格冲突，这是历史遗留的严重败笔，但广场空间的阔大，削弱了建筑物之间的这种不和谐性。但新的金红色立柱却以强行插入的方式，排列于大会堂和博物馆前端，加剧了这种原有的风格对抗，令这座庄重的祭奠广场，沦为由一堆彼此对抗的碎片混搭成的游戏场，进而成为针对所有死难者的一种嘲弄。<br />
&nbsp; &nbsp; 以天安门城楼上的检阅者的身份俯察56根立柱，其尺度完全合乎宫式建筑的范式：远距离的透视缩小了立柱的体量，于是它们必须足够高大，才能跟广场的宽阔性密切呼应，藉此表达威权政治的宏大特性。但要是以一个普通游客的身份向上仰望，就会发现立柱的体量与人的体量之间的悬殊差异。13.6米的高度，1.8米的直径，26吨的重量，加上密集排列的数量，加剧了天安门广场原有的“蝼蚁效应”，令所有游客都变得渺小起来，仿佛是爬行在旷野上的虫子。<br />
&nbsp; &nbsp; 这是典型的威权主义美学，它要以贬损个人的存在来反衬权力的伟岸性。而正是这种夸饰的权力美学，彻底颠覆了设计者所宣称的“人民做主”的原则。不仅如此，当太阳升起的时刻，只有6米间隔的立柱及其阴影，就会形成粗大的栅栏，把游客关入一个类似囚室的场所。这真是一种奇妙的场景，权力建筑制造了戏剧性的光学效果：在早晨和黄昏，巨大的柱式阴影压在游人身上，犹如阴郁而沉重的枷锁。<br />
&nbsp; &nbsp; 这是权力美学晶体所折射出的多重镜像，但还不是民族柱的核心语义。作为权力的伟大象征，56根巨柱就像被征集入京的权杖，代表着不同来自民族、区域和阶层的意志。它们在天安门广场的集体亮相，显示出捍卫中央集权的信念。面对群体事件和民族冲突，安全焦虑变得日益炽烈，以致它必须转换为鲜明的建筑诉求，并在最重要的集会空间里予以公示。民族柱摆出了拱卫天安门城楼的雄姿，喻示着对北京的忠诚，而这正是“和谐社会”的重大目标，也是56根巨柱的命运。它们是甲子中国的守望者，去远眺变幻多端的外省风云。</div>

							
							
							
							
