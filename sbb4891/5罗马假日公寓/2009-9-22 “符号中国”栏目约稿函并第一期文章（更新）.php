<br />
<br />
博客：“人生如寄”的隐喻<br />
<br />
&nbsp; &nbsp; Log的原意是航海日志，是“记载舰船航行情况的文书。是分析航行情况，总结航海经验、积累航海资料的主要依据，审理海事时局有法律效力的原始记录”（《辞海》）。因为航海日志具有法律效力，写上就不允许涂改，所以如果写错了，也只能在后面加注修改。海船是每天中午12点交接班，因此，每天的航海日志记录的是前一天中午12点到当日中午12点之间发生的事情，这就要求航海日志要每天撰写。而blog（Weblog）一词是从log延伸而来的，中文全译是“网络日志”，是指每天在网络上记载的日志。<br />
&nbsp; &nbsp; 虽然大陆和台湾对blog一词的音译有很大的不同，是因为受不同英语习惯的影响，但是这两种音译的区别却饶有趣味，从中可以见出两地不同社会环境、不同文化环境的潜在影响。台湾音译为“部落格”，是一种中性的译法，“格”类似于猜谜中“卷帘格”、“秋千格”中“格”的用法，指一种格式。既然只是一种格式，那就只是纯技术性的，并不附加任何更丰富，更富有文学想像的含义。“部落”一词更是一种客观描述的中性词。而大陆音译为“博客”，就与“部落格”的音译方式截然相反，加进了许多文学想像的成分。“客”，是一个含义丰富的词，本义是“寄居”；“博”的本义是“大”。这样，“博客”这个词就变成了“在广大的地方寄居”的意思。<br />
&nbsp; &nbsp; 因此，方兴东博士发明的“博客”这个词非常隐秘地变成了我们生存环境的隐喻。<br />
在汉文化语境中，与“客”密切相关的是“宅”和“舍”这两个字，比如成语“魂不守宅”，也写作“魂不守舍”。“宅”和“舍”字大有讲究，虽然都是住所之意，但是语义绝非如此简单。《说文解字》：“宅，所托居也。”此处的“托”是一个很重要的字眼，“托”即寄托，因此“宅”又引申出存、寄托、顺应的意思，“宅心仁厚”即存心仁厚，“宅生”即寄托生命，“宅天命”即顺应天命。《说文解字》：“舍，市居曰舍。”市居即客店，客店是供人暂时寄宿的处所，因此同“宅”一样，“舍”也有“客”、寄居、寄托的含义。“寄居”就是这一传统文化的深层心理。古人认为，人生是寄居在这个世界上的，这个世界并不是我们的家园，也不是最终归宿，它只是一个暂时寄居的地方，一个暂时使用的壳儿，总有一天我们会离开它。汉语中有无数这样的表述，比如“人生如寄”，比如“人生苦短”，比如“人生如白驹过隙”，比如“人生寄一世，奄忽若飙尘”，比如“生命如寄旅”……正因为有这样的深层心理，这样的民族潜意识，所以古人才会对时间的流逝，才会对生命的消逝那么敏感，那么低回惋叹：“人生不满百，常怀千岁忧。”“逝者如斯夫，不舍昼夜。”“人生天地间，忽如远行客。”“人生忽如寄，寿无金石固。”<br />
这样的民族潜意识引发的结果异常复杂，直观地说，它拉长了中国人的时间观，使他们淡漠眼前的苦难和幸福，同时也使他们视个人的遭际为当然，从而可以以平常心咏叹或者玩味这种遭际，进而上升为惟中国人才能理解的“身世之感”、“家国之感”。中国文化之外的人不会有“身世飘零”的感慨，也不会有家国沦落的切肤之痛。<br />
当然，这也导致了国和家这样的大词凌驾于个人、自我这样的小词之上，个体价值和公民意识不易得到培养。“国家”这个词越俎代庖而僭居为本源。本来是派生的物事，本来是便宜行事的物事，本来是人们为了节省精力而迫不得已选择的物事，反而变成了一个庞然巨兽，压倒性地蹲踞在把一部分权利让渡给它的人们的头上，并自我编纂了一整套合法性法典，反过来要求人们遵从。人类政治史常识的关于“国家”概念的界定，在个人主义视若洪水猛兽，集体主义僭居正统地位的国度里，早已成为习焉不察的爱国口号。<br />
&nbsp; &nbsp; 在这样的语境中，每个具体的个人遂一变而为“国家”这一宏大叙事的寄生物，人，成为寄居的一类生物，这一信念深深地积淀在了中国人的心灵深处。<br />
在国家主义占据主导地位的国度里，在“锦衣卫”传统和“道路以目”传统深厚的社会中，供寄居的个人发泄的精神出口少之又少。因此，当伴随着意识形态退潮，市场经济覆盖一切领域，尤其当伴随着全球化而来的的互联网进入中国日常生活的时候，blog这一全新的网络工具，立刻成为一个千载难逢的精神出口。人们纷纷开设自己的博客，把它当作从所未遇的寄居载体。这可以用来解释西方媒体的一个困惑：为什么博客在西方只是一小部分人自娱自乐的工具，而在中国，却形成了一股巨大的热潮。因为在西方文化语境中，“天赋人权”，个人价值是一切价值的源头，根本不存在精神出口的概念。而在中国，这却成为惟一的精神出口。因此，在这个精神出口里，蜂拥着大量的个人隐私―肉体的展示，性欲的赤裸裸的表达―以及大量的政治隐喻和权利诉求，大量的对所谓“内幕”和“黑幕”的揭露，从而成就了最具有中国特色的博客风景。<br />
&nbsp; &nbsp; 方兴东博士有一句访谈，提供了这一风景的最雄辩的论据：“目前blog还处于一个比较初级的阶段，使用blog往往是一些持非主流观点的人，如前一时期非常轰动的木子美。”“持非主流观点的人”更需要精神出口，他们迥异于世俗的意见、观点和行为更需要有一个接纳的地方，这个地方就是“博客”。<br />
&nbsp; &nbsp; 这些“非主流观点”还包括张钰的性交易录像事件、饶颖诱奸和强奸日记事件、黄健翔辞职内幕事件。这些无法在公众媒体曝光，无法被主流口径容忍的事件，只好栖身于博客之中。这是一种心不甘情不愿的“寄居”。他们其实不愿 “寄居”，他们想做自己的主人，但他们必须“寄居”。在日常生活中，“博客”有一个绝佳的对应词―“暂住证”，你仅仅只是暂时住在这儿，哪怕这是你的祖国，你也只是“暂住”而已。他们其实不愿“暂住”，但他们必须去办理“暂住证”。<br />
直到那一天―“客人”变成“主人”的一天，“博客”变成“部落格”的一天，我们才能够从大一统的铁拳之下挣脱出来，变成散落的真正的个人，发散出每一个具体的个人价值的迷人光辉。<br />
