																											<div id="postmessage_324345" class="t_msgfont">朱翠：玫瑰的隐喻<br />
<br />
<br />
情爱学里，玫瑰具有双重隐喻。但是，人们提起玫瑰，往往第一意识到的便是盛放玫瑰与女人的单一关系。小资鼻祖张爱玲，就借用这一通俗含义书写了小说《红玫瑰与白玫瑰》，并以玫瑰颜色之不同，来喻指性格迥异的两类女子。<br />
花类植物喻指女性，在民众的观念里根深蒂固，这与女人在历史语境中的具体地位脱不了干系。古典女子居于深闺，她们的优美，纤细，静雅，不事劳作，皆与植物有着完全相类的观赏意义。她们活着的根本目的，便是被男人当花朵一样种植，养护，盛放，欣赏，观看。她们以消耗脂粉、饰物、衣履、金钱为日常生活的全部教义，并籍此来修饰着自己花般的容貌，以便得到赏花人的宠幸。一旦鉴赏者的眼光离开，她们就彻底不存在。她们的生死，摇摇摆摆、危机四伏地悬挂在男人的目光之间。三寸金莲就是这目光灌溉下的畸形产物。所谓莲步轻移、一摇三晃、如风摆柳等等描绘女子形态的词汇，皆是植物才具有的特征。植物若女子一样娴静，无法行走，女子若植物一样可以随便摆布。<br />
众所周知，花朵是植物裸露在外的绚烂生殖器。而植物性的女子，对男性而言，便是最佳的情欲消费对象。男人常言“这个女人充满了女人味”。女人味指的是什么呢？指的就是植物味，花朵味，情欲味（古代名妓就是这方面的最佳范例）。作为一个作家，张爱玲意识到了女性若花的通俗意义，但没意识到对胡兰成言“我见了你，变得很低很低”，以及“她是枯萎了”诸如此类的用语，本身便是自我植物化的症候。这注定了她年轻时抛出的一片深情，只是一个易凋的花期。<br />
其实，玫瑰还含有另外一层至为重要的隐喻。很多大作家隐约地意识到了它，但皆不曾挑破窗纸。巴尔扎克认为“任何一朵玫瑰都是一盏灯”。这是对蕴满光与热的植物花蕾，进行的第一次物理学角度的赞美。乔治&#8226;罗登巴赫则把巴尔扎克的比喻倒置过来，他说“房间里的灯是一盏白色玫瑰”。这是两个绝妙的抒情性比喻。更妙的在于，这两个比喻将玫瑰与灯进行了互喻。而日本诗人金子光晴，将这隐喻推得更远，他这样戏语他的神甫朋友，说他进入天国后，纯洁的肉体将“提着灯笼去见上帝”。这句诗里的“灯笼”指的恰恰是男性阳具。自此，玫瑰花（植物生殖器）―灯笼（光与热的载体）―阳具（男性生殖器），三点一线，犹如三粒珍珠摆放一起。玫瑰隐喻谱系的另一面，就此昭然若揭。<br />
《红楼梦》第六十回，芳官把一瓶得自宝玉的玫瑰露给了五儿，惹出一场丫鬟婆子们争风吃醋的风波来。小小玫瑰露，怎地惹来如此大的麻烦呢？《本草纲目拾遗》中说，玫瑰露能和血平肝，养胃宽胸散郁，看来药用价值不低。但此处有趣的不在于玫瑰露的药用价值，而在于这玫瑰露是大观园里惟一的公子哥儿的赠予，这赠予对大观园的众多丫鬟而言，本身便是一服男性之宠的药剂。<br />
安徒生写过一篇鲜为人知的童话《最美的玫瑰花》，里面的玫瑰花亦以药的面目出现。故事讲的是一位喜爱玫瑰花的皇后大病不起，医生说：“惟有送给她一朵世界上最美丽的玫瑰花―一朵表示最高尚、最纯洁的爱情的玫瑰花。这朵花要在她的眼睛没有闭上以前就送到她面前来。那么她就不会死掉。”她的臣民为她寻遍了所有种类的玫瑰，却皆不能将皇后的疾病治愈。最后皇后在一本书里找到了她要的玫瑰。“她的脸上露出一片玫瑰色的光彩，她的眼睛变得又大又明亮，因为她在这书页上看到世界上最美丽的玫瑰花―从十字架上的基督的血里开出的一朵玫瑰花。”基督之血，最伟大的男性之血中开出最美的玫瑰。<br />
美国作家福克纳有一篇著名的短篇小说，叫《献给艾米莉的玫瑰》。整篇小说不见一朵玫瑰，只见被岁月腐蚀的玫瑰色。“败了色的玫瑰色窗帘，玫瑰色的灯罩”―败了色的玫瑰色，是变异的玫瑰色，是扭曲的情欲的颜色。这惨不忍睹的颜色，年久日深地藏在一个变态女贵族艾米莉的婚房里。艾米莉毒死了她爱的何默，因何默不肯与她结婚。数十年里，死掉的何默，不仅阳具属于她，连整个肉体皆献给了她。一朵腐朽的死玫瑰，恒久地盛放在艾米莉生活的老屋里，散发出臭气熏天的被扭曲的情欲气味。<br />
千百年来，一到情人节，男人们皆送自己深爱的女子以含苞玫瑰，用来表达爱意。玫瑰朵朵，香气馥郁，花瓣紧拢，状若酒杯，与男性勃起的阳具，呈现着何等惊人相似的容貌。每次看到这些花朵，我便想探寻它的隐喻。从人类交换的角度而言，这些蓓蕾的玫瑰，隐喻的绝对不会是女性。那么它隐喻着什么呢？直到某一天，我才蓦然明白，它隐喻的恰恰是男人，亦是男人的灵魂与肉体。而每一朵献给女子的蓓蕾玫瑰，表面上表达着男人们这样的话语：亲爱的，我把玫瑰献给你！潜台词却是：亲爱的，我把玫瑰献给你，我把我的阳具献给你！</div>

							
							
							
							
