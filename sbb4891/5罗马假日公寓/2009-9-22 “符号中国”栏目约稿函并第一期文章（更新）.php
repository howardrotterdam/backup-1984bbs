																											<div id="postmessage_324340" class="t_msgfont">唯色：希德废墟的前世<br />
<br />
有座废墟，它在拉萨，我很想给它写一篇类似于编年史的文字。但编年史过于学术，不如改成简历吧，就像我们每个人的简历，别人看着枯燥，自己写着容易。<br />
废墟也有生命，有它的前生后世。可为何那么多的废墟，偏偏念念不忘这个废墟？我说不清楚，总觉得自己似乎跟它有点什么关系，特别是每次赶去看它，看着它日渐荒败，仅剩下颓垣断壁上的几根残梁笔直地刺向天空，透着垂而不死的挣扎劲儿，而那残墙上无比惊艳的壁画越来越破损，如同绝世美人尚未迟暮，却被不治之症吞噬，徒令亲人哀恸无比。是的，我就像是这废墟的亲人，或者说，我也许有一两个前世，是与这废墟的前世一起度过。<br />
每次去这废墟，每次都能看见孩子。是不同的孩子，只有他们喜欢在废墟周围玩耍，一看见举着相机的人来，马上摆出各种各样的姿势。小男孩，小女孩，全都乐意让人拍照，似乎在这片废墟前留影是他们最开心的游戏。有一次，跑来两个小男孩，执意要带我往里去，那是我第一次走进废墟，蓦然看见残墙上紧靠着一个巨大的塑像，嗯，是那大威德金刚（梵名Yanmāntaka，藏语“多吉久谢”，简称吉吉，意为“怖畏金刚”。藏传佛教认为是文殊菩萨的忿怒相，乃修行的本尊之一），可除了泥土、草垛和木棍，只留下巨硕的牛头怒目圆睁，以及无数只残缺不全的手臂。那时是黄昏，金黄的光线下，每一根弯曲的手指倒很完整，似乎会说话，似乎很是可怖，我着实给吓了一跳。以后再去，我都会轻手蹑脚地去看看吉吉，看着它头颅滚地，碎成几瓣；看着它手指一节节断落，终于有一天什么也没有了。所以当我听说，吉吉的某只手臂是被离废墟不远的酒吧老板给折断的，然后放在吧台上当作装饰炫耀，我特意寻到那个酒吧，不巧没开门。据悉老板是个“藏漂”，来自汉地什么地方我忘记了。还听说，废墟里残破的壁画也被游客从断墙上剥落，盗走，周遭的博巴（藏语，藏人）居民风闻而至，生气地用驱魔的方式冲着这些人使劲地抖落邦典（妇女的服饰，系在腰上的五彩围裙），将其撵走。<br />
还有一次，在废墟的石阶上遇见一个小女孩，她不像一般的孩子又是雀跃又是作态，她根本不，她凝视着或者说是紧盯着我的相机，一只手托着腮，就那么紧盯着镜头，神情里有一种不寻常的成熟，最后反倒是我心慌了，而给她拍摄的照片，后来发表在《南方周末》的地理版上，黑白两色，透着沧桑之美。还有一次，离废墟不远，停放着一个太阳灶，正迎着火热的太阳烧着一壶将要沸腾的水。太阳灶旁边坐着一位晒太阳的老妇人，她像是怀着满腹心事，手上的念珠有一阵没拨动了。而在她的身后，晾晒着花花绿绿衣裳的绳子，竟然跟好几串已经陈旧的经幡，全都拴在废墟的门柱上。微微的风把经幡吹得呼啦啦地招展，但刚洗好的衣服很沉，把晾衣绳压得很弯，水珠儿还在滴落，这一切多么地日常生活啊。<br />
前年又去废墟，见环绕废墟的两层楼房已经翻新，涂红抹绿，做成藏式风格，出租给各种各样的人或人家，有来自藏地各处的，也有汉族包工队或回族商贩。对于忙碌着世俗生活的他们来说，这废墟，远不如二楼拐角的公共厕所或者院落中间的自来水更重要，可在从前，那两层楼房是众多僧人栖身的僧舍啊。<br />
是的，这废墟的前生是寺院。确切地说，它不是寺院，它属于靠近澎波（藏语，今划入拉萨东北面的林周县内）地方的热振寺，名为希德扎仓，俗称希德林。希德是和平，扎仓是经学院。多亏那些个在1950年之前不时在拉萨晃来晃去的“帝国主义分子”，给希德扎仓至少拍摄了几十张照片，足以留下当年盛到极致的景象：法会庄严，喇嘛云集，金刚法舞惊世骇俗，四四方方的白帐篷顶绣着吉祥云彩，完整无缺的庞大殿宇称得上美轮美奂。<br />
好了，我得给希德扎仓写一份简历。既然它不说话，趁它还未完全化为乌有，大概讲讲它的前世。<br />
听说希德扎仓是拉萨最早的几座佛殿之一。早到什么时候？赞普（藏语，天子，君王）松赞干布那年代吗？有的资料说是，也有说是稍后的岁月，总之可以归于图博（通常被写成“吐蕃”）时代。主供佛不知。或是吉吉？吉吉是文殊菩萨的忿怒相，掌管无上智慧，但须得作狰狞状才能让智慧像惊雷打得脑袋开窍。<br />
14世纪以后，希德扎仓属于热振寺。热振寺因为有一世热振仁波切是甘丹颇章政权的摄政，而变得很著名。希德扎仓最盛时有400多僧人，尤以羌姆（藏语，金刚法舞）颇负盛名。看似一场场乐舞，却是一场场法事，所以必须由僧侣自始至终。威严的法乐，庄重的舞姿，灿烂绚丽的服装，神秘莫测的面具，却在日落时分散发着繁华散尽的悲哀，渐渐地从众僧的心头袭上眉头，但他们并不察觉。盛极必衰，如今它的名字是希德废墟，其实它是拉萨最醒目的伤疤之一，迄今仍然裸露着令人惊悸的伤口。<br />
希德扎仓毁于“文革”。旧人去，大屋空，而鸠占鹊巢的，当然是远道而来的革命者和刚刚“翻身”的群氓，其中有金珠玛米、豫剧团的演员、红卫兵、造反派和积极分子。他们把一个巨大的高音喇叭装在最顶上那层，于是希德扎仓成了“造总”（全名为“拉萨革命造反总部”，拉萨两大造反派之一）的广播站。我采访过当年的一位红卫兵，如今他是有名的民俗学家，回忆说：“当时我们的小宣传队就住在希德扎仓里面，除了演出我们就坐在院子里不敢出门，也不敢回家，怕在回家的路上被对方那一派抓住打个半死不活的，算你倒霉。那个时候很厉害，到处都是广播喇叭在响，而且还有宣传车。那边哇哇叫，这边哇哇叫，尤其是军区的大喇叭，那个时候的拉萨可不得安宁了，简直是！”据一位住在希德扎仓附近的老先生回忆，只要那喇叭朗诵毛主席的“最高指示”或者唱“大海航行靠舵手”，整个老城就像在滚滚而来的雷声中东摇西荡，以至他落下了一听到那种尖利、亢奋的声音就会心悸得快要晕厥的毛病。广播站是造反派非常重要的宣传阵地，于是希德扎仓变成了造反派互相攻打和争夺的战场。你扔一个自制的手榴弹过来，我就连射无数发机枪子弹。当战火硝烟散去，曾经有过五层之高的佛殿已被削得只剩三层。僧人？僧人早就跑的跑，抓的抓，杀的杀，正好空出房间留给金珠玛米一住就是多年。希德扎仓成了军营，就像祖拉康（藏语，大昭寺）成了金珠玛米的猪圈。<br />
十多年前，希德扎仓的内墙上还看得见很清晰的毛主席语录，那是“团结紧张，严肃活泼”，太熟悉了，让我重返我被洗脑的学生时代。可是在巨大的、血红色的字迹之间，仔细看，竟然用粉笔画着一个手拿大刀、露着生殖器的男子边狂奔边回首，而楼上，整整一面墙上还留着斑驳陆离的壁画，依稀辨认得出戴黄帽的宗喀巴大师双手结印，跏趺而坐。<br />
说起来，希德扎仓的下场不过在半个世纪内完成，耐人寻味的是，竟然就到了众说纷纭或者讳莫如深的地步。一些专事改写历史的文人，不愿意提及“人类杀劫”留下的累累凭证，一言以蔽之地说，早在图伯特被和平解放以前，那个当过摄政的热振活佛是“爱国人士”，他被亲英帝国主义的达札活佛迫害致死以后，希德扎仓就被烧毁了。这可真的是一派胡言啊。那个被认为是“爱国人士”的热振活佛的转世，是我一位表姐的丈夫的舅舅。他的一生太悲惨了，比前世更悲惨，还在十多岁时，就被收编进什么“青少年活佛班”，听上去不错，其实是劳动改造，11个少年朱古（朱古，藏语，转世活佛，又称仁波切）全被弄去牧羊放牛，养猪捕鱼，用“六六六”药粉杀虫子，而且他们经常挨饿，只好偷吃掺有酒糟的猪饲料，结果都有了酒瘾，直到1978年才被“落实政策”，但其中一些朱古的一生已经给毁了。比如热振仁波切，变得嗜酒，最后疯癫致死。又比如他前世的敌人达扎仁波切，这一世是个酒鬼、烟鬼，长了一个红彤彤的酒糟鼻子，曾喝醉了在佛协提着打猎的小口径步枪满院子追撵老婆。总有一天，我要写写那11个朱古的坎坷今生，那才是活生生的佛法示现，比任何的当头棒喝更有力。<br />
总而言之，寺院变成战场或者军营、据点，比寺院变成旅馆、仓库更加触目惊心。应该说，图伯特从来没有出现过这么多的废墟，哪怕是在教派有过纷争的历史上。而这些废墟都是近代蜕变的象征，有当年不远万里来到拉萨的“帝国主义分子”的图片为证，多亏他们，用难能可贵的影像留下了许多废墟的前生往世。我还见过一位1960年代当兵进藏的汉人在他30多年的西藏岁月中，为热振寺在不同的年代拍下的不同照片，第一张寺院犹在，而第二张就夷为平地了，而第三张，唉，幸好有这第三张，不然这世界真的令人绝望。在第三张上，一个虽然远远不及当年规模，却俨然是一座正式的崭新的寺院大殿，已经向游客开放了。然而纷至沓来的游客亦不可小觑，一位坐火车去拉萨拉萨旅游的北京老人兴奋地说：去那么多人，再高的海拔也会给它踩平喽。<br />
所以希德扎仓的今天，不必多写了吧。反正像我似的总惦记着，去如今的废墟前作凭吊状的人不会太多。听说有些聪明的脑瓜早在打这废墟的主意。因为它占据的位置实在是好上加好，那么大的地皮，如果就地盖个多少颗星星的酒店，不尽财源不滚滚来才怪。或者盖成大杂院，五层或者六层，租给源源不断的包工队和“巷盅麻”（藏语，妓女），也是一笔厚财。跟祖拉康一样高又怎么了？不让跟祖拉康一样高，那是旧西藏的破规矩，需要像“文革”破“四旧”那样给破除掉。据说在旧西藏，站在祖拉康的二楼上，可以放眼远眺东南方向的色拉寺，但几年前修建的著名援藏工程―拉萨市公安局科技信息大厦，阻挡了从祖拉康望见色拉寺的视线，被公认是拉萨超高违章建筑的典型。有一度，祖拉康凭借“世界文化遗产”的名声和联合国教科文组织这把看似唬人的大伞得以维持传说中的风光，可惜风光不再，如今在祖拉康的西北面和北面，已经有两座巨型商场的高度超过了它。也因此，我有预感，他们是不会让希德废墟像一个大伤疤似的总摆在闹市当中让他们难堪的，等着瞧吧，或许要不了多久，这里将平地起高楼，再过些年，若提起希德，什么？什么？你说谁呢？<br />
我见到这废墟的最后一眼，准确地说，是到目前为止的最后一眼，发生于2008年8月，其实只是一瞥，足以让我心惊胆颤。就在去往希德扎仓的路口，可能只能容一辆三轮车出入的路口，站着四个钢枪在手的武警。渐行渐近，可以清晰地看见年轻的他们面带凶相。他们是在守护里面的废墟吗？或者说，那废墟，因为外面有了持枪的他们而变得意味深长。我本想进去看看废墟是否更加倾颓，犹豫间，只留下了十分模糊的一瞥。此刻多少有些后悔，多年来，我一直在观察的那座废墟，错失了2008年的记录。</div>

							
							
							
							
