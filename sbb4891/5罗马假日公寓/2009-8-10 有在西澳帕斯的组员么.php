转个介绍文章<br />
　帕斯是占澳大利亚全土三分之一的西澳州的首府，也是充满活力的城市。这里是度假胜地，阳光和海洋是她的标志。<br />
　　帕斯位于澳大利亚西部，印度洋沿岸，是世界上最孤独的城市。从地理位置上看，帕斯西面是浩瀚的印度洋，沼纬度线西行7350公里到达非洲海岸，南面是远隔重洋的南极洲。北方最近的港口雅加达距离也有3l50公里，东面是大陆，但是如果想到达另一个城市阿得菜德要穿过沙漠，最近路线也要2740公里。<br />
　　尽管偏僻，帕斯发展却欣欣向荣。这座城市正式创建干19世纪初，由于在内地发现了黄金，到19世纪末期，移民开始增加，进入20世纪，随着金矿业不断发展，帕斯也发展成为一个现代化新兴都市。帕斯于1829年成为自由移民的殖民地，她虽利用囚犯的努力来进行建设，但是，促使帕斯与澳洲西部蓬勃发展的原因还是十九世纪末的淘金热。目前帕斯以其丰富的地下资源为后盾，继续发展成现代化都市。在西澳，能够与“都市”称呼相匹配的城市只有帕斯，与很多城市聚集的东海岸相比，影响力比较弱。也许是因为这个缘故，短期旅行者很少有来这个城市的。但是只要去过一次帕斯的人，都会被这位于天鹅河畔的美丽城市所吸引。<br />
　　斯旺河发源于东部，由东向西流向大海，在经过帕斯市，聚集起来形成一个较大的湖泊。河口直抵印度洋。斯旺河把帕斯分成南北两岸。南岸是自然保护区，北岸是商业区。斯旺河年复一年向西流去，南岸度假的人们有的钓鱼，有的观赏黑天鹅，蓝色水面上成群黑天鹅自在地游来游去。<br />
　　据土著人传说，西澳波布尔门部落有一分支的祖先是由黑天鹅变来的。黑天鹅仅产于西澳洲，因此成为西澳洲的象征。人们一般熟悉白天鹅，对黑天鹅知之甚少，西澳州的黑天鹅，除了翅膀上有几片白色或褐色的羽毛外，全身都是漆黑的。泛出一种漆的光芒。它的喙颜色鲜红，足部暗褐色，这种颜色的自然配台，越发显得黑天鹅的高雅华贵，不同凡响。<br />
　　这些黑天鹅永远以西澳州为家，并不象白天鹅一样迁徙。当地居民愈发喜爱它们，为它们不断创造良好的栖息和繁殖环境。黑天鹅越发与人熟悉，站在岸上的经常围住游人，索要食物，与人逗乐。黑天鹅的形象已经成为西澳洲标志，帕期也被称为“黑天鹅城”。在街道两旁的招牌上随处可见，所以游客到了帕期是一定要到河边和黑天鹅合影留念。<br />
　　斯旺河和辽阔的印度洋为喜爱水上运动的居民和旅游者提供了广阔天地。绵延数英里的白色海滩以及附近一些小岛成为受人欢迎的度假胜地。在离市区8公里处，有一大峡谷，其中有两块巨石，高出海平面8米，巍然屹立激起千尺浪花，发出震耳的啸音，令人惊心动魄。良好的气候和美丽的风光，使这里成为新兴的旅游城市，世界各地的游客和许多当地澳洲居民多喜欢来这里度假。沿海堤散步，海浪惊涛拍岸，海风吹得人满脸都是头发，海鸥肥嘟嘟的，轰它，不动；再轰，慢慢往前踱两步；三轰，才颇给面子地低飞两下，一副懒得跟你计较的神情。<br />
　　帕斯市中心也有许多公园和花园。最大的国王公园位于西郊，占地404公顷，它是一片天然丛林和地势起伏的草地，内有珍禽异兽、春天野花盛开，把公园点缀得五彩缤纷。除了国王公园之外，还有九个国家公园。<br />
　　帕斯附近还有著名的卡尔古利金矿和皮尔巴拉铁矿，这条线路是喜爱探险者的去处，既能体验旷野生活，也可以顺路发掘一下宝藏。<br />
　　每年8月到11月是柏斯的花季，没有边际的保护区里，鲜花以从来没见识过的豪放气派，展示它的魅力。将野花制成押花嵌在画框中成了帕斯有名的礼物。<br />
　　外人说西澳(West Austria)的缩写“WA”其实是Wait A While(等一会儿)的意思，嘲讽他们生活闲散，办事效率低。但看看珀斯吧，干爽的气候，灿烂的阳光，心醉的河湾，富足的生活，如此良辰美景怎么能够辜负了呢？正是要等一会，多看一眼呢，莫负这风光。<br />
　　对于大部分人来说，悉尼就是澳洲，这多少令西澳的美丽有些气馁。其实，美景如同美女，常常会养在深闺人不识，偶露芳姿，旋即惊艳，令人魂牵梦绕。<br />
　　帕斯给人的感觉是开阔、明亮。游客可以去参观帕斯铸币厂，亲自动手铸个硬币或纪念章；或者去帕斯动物园，或在澳州种类最多、最茂密的棕榈树林中松弛身心。<br />
　　帕斯市以其众多的美术馆、野生动植物和主题公园、白色沙滩、印度洋冲浪海滩、以及观赏海豚和鲸鱼的机会而自豪。下游是充满活力、历史悠久的港市弗莱曼特。<br />
　　帕斯市周围有各种引人的好去处。你可以去参观坦布尔嘎姆农场，那里有农庄展和土著表演；去皮尔地区欣赏丛林及大自然的景色，如果你属于好动型的，可以去打高尔夫球或参加水上运动；从达令阿普乘有轨电车去探索亚拉森林，如果你是火车爱好者，可以去乘蒸汽机火车或其它古老的柴油机火车；到雷因普尔自然保护区的墨累河畔露营，这里也是划独木舟和丛林散步的理想去处；或者去西澳州最长的小径比布尔曼路远足。<br />
　　夜晚，你可以去位于美丽的公园中、昼夜开放的百事活赌场一试运气。<br />
<br />
天鹅河（SWAN RIVER）<br />
　　美丽的珀斯由一条天鹅河（SWAN RIVER）分为两部分。河北岸的圣乔治大道（ST GEORGES TERRACE）是珀斯金融、公司和政府机构的集中地，有办公街之称。圣乔治大道北邻的海伊步行街（HAY STREET MALL）则是珀斯的商业闹市中心。这两条街均为东西向，夹在中央火车站和天鹅河之间。所以，从中央火车站往南向天鹅河方向行走的话，就是在珀斯的心脏内走动。<br />
<br />
蒙格湖（Lake Monqer）<br />
　　位于市区北面，约10分钟车程，可见到上百只活泼的黑天鹅悠游其间。<br />
<br />
国王公园(Kings Park)<br />
　　国王公园是南半球最大的公园。公园里有一株平躺着的粗壮而古老的树干，是百年多以前，西澳大利亚早期拓荒者从遥远的北美洲加拿大运来的。在这里可俯瞰帕斯市区，园内有幽静的行人道，和占地12公顷、以春季花卉展览见称的植物园，是极受欢迎的野餐区。若从市中心前来此地，可选择步行，或搭乘便捷的帕斯电车(Perth Tram)。在此享用午餐或野餐，将是极大的享受。<br />
<br />
弗里曼特尔（Fremantle)<br />
　　在帕斯市西南19公里处，建于1892年的弗里曼特尔正好位于天鹅河河口，是个非常繁忙的海港。交通很方便：巴士、火车、渡轮与计程车都可到达。弗里曼特艺术中心（Fremantle Arts Centre）、海事博物馆（Marltime MuSeum)和圆屋（Round House），也值得一游，不容错过的还有本地的集市与钓鱼船港（Fishing Boat Harbour）。旅游本地的最好方法，就是在南方大道（South Terrace）的咖啡道（Cappuccino Strip），找家露天咖啡座坐下感受当地的小镇风情，并欣赏街头艺人的表演。不要忘了尝尝本地的可口啤酒，诸如Redback和Doqbolter。<br />
<br />
帕斯铸币厂(Perth Mint)<br />
　　位于Hay街，游客可参观钱币铸造、金币融铸过程，并可亲自动手铸币或造纪念章。<br />
<br />
凯维森野生动物园（Caversham）<br />
　　位于帕斯以北20公里的西天鹅区，您可以去那儿看树熊，袋鼠和其它动物。<br />
<br />
玛格丽特河（Margaret River）<br />
　　风景如画的玛格丽特河位于帕斯以南280公里处，以酿酒场、冲浪海滩、附近的石灰岩洞穴、游泳、钓鱼、风景优美的驾驶路线与一年一度Leeuwin Estate音乐会著称。<br />
<br />
帕斯的海滩<br />
　　距市区仅12公里，搭乘公共交通工具即可到达。帕斯的冲浪海滩(surfing beaches)达到世界级水准，离市区最近的是City Beach，而Cottesloe、Floreat、Swanbourne(天体海滩)、Scarborough和Sorrento海滩也很受欢迎。来到这里，可以享受日光浴、游泳、冲浪与印度洋美丽的落日。<br />
<br />
苏伦多码头（Sorrento Quay)<br />
　　在Hillarys Boat Harbour，是一个出色的地方，来此可享受帕斯宜人的天气与美丽的海滩，也可以进行水上活动、逛逛新潮的咖啡厅、享受美食与许多游乐设施，从事水上活动、骑自行车、乘Harley Davldson摩托车兜风、潜水和深海钓渔。<br />
<br />
帕斯动物园（Perth Zoo）<br />
　　位于Labouchere路，South Perth，坐落于美丽的花园中，是个让人松弛身心的好去处。林荫步道、绿油油的野餐区，再加上澳大利亚最茂留的棕榈树林，营造出舒适的空间，轻松地观赏动物。<br />
<br />
屯保甘农庄（Tumbulgum Farm）<br />
　　位于South Western Highway，Mundijong。离帕斯50公里，为一座精心设计的观光农场。内有可容纳750人的会议厅、250个座位的接待中心，及可眺望广阔海岸平原的大型农场。农场内有野牛、鸸鹋（emu）、鹿、袋鼠和马。另外，这里还展出大量的西澳产品，并有土著人文化表演，包括corroboree传统舞蹈与掷回力镖。农场表演节目还有：剪羊毛、召回羊群、鞭术表演、挤牛奶、乘稻草车、享用澳大利亚传统的比利茶（billy tea）与丹波面包（damper）。<br />
<br />
帕斯海底世界（Underwater World Perth）<br />
　　位于Hillarys Boat Harbour，West Coast Drive，Hillarys。漫步于压克力纤维制成的海底隧道时，游客可以观赏到2000种以上的海底生物，包括沙锥齿鲨（grey nurse shark）、黄貂鱼及许多其他鱼类。合格的高空跳水者会与鲨鱼及大鳐鱼（giant rays）一起在水族馆的主馆表演。这里的海豚庇护所为红鼻海豚（bottlenose dolphin）提供了一个家。<br />
<br />
冒险世界（Adventure World）<br />
　　位于Progress Drive，Bibra Lake，距帕斯市南面约16公里，是一座得奖的主题乐园。园内有30种精彩的游乐设施，包括海滩、河中游泳、喷泉泳池、冒险城堡、神秘洞穴与秘密海滩。乐园开放时间：9月至隔年4月，每天上午10:00至下午5:00。<br />
<br />
布鲁姆（Broome）<br />
　　位于帕斯北方2213公里处，是个拥有美丽海滩的采珍珠中心。这里有座中国城，以及一座日本人为采珍珠的丧生者而立的纪念碑。来到这儿，一定要到距布鲁姆6公里的Cable Beach，好好松弛一下。参观Willie Creek的珍珠场，或乘坐四轮驱动车至Windjana和Geikie Gorges一游，都是很不错的决定。<br />
<br />
罗塔纳斯岛（Rottnest Island)<br />
　　离弗里曼特尔海岸18公里，以沙袋鼠（quokkas，一种类似袋鼠的小形有袋动物）、美丽海滩与富含海洋生物的干净海水闻名。这里可以从事的活动包括游泳、浮潜、钓鱼、风帆船、高尔夫与登山。脚踏车是主要的公共交通工具，每天都有渡轮往返于弗里曼特尔和帕斯。<br />
<br />
企鹅岛与海豹岛（Penguin and Seal Islands）<br />
　　位于帕斯南方海面42公里处，企鹅岛在近海海面600公尺处，渡轮可达，是许多野主动物、海洋生物的家乡，拥有海岸线景色与解说中心。往返于企鹅岛和海豹岛的渡轮，从沙洲航海公园（Shoalwater Marine Park）出发。在这里可以看到小企鹅、海狮、海豚、稀有鸟类、特殊洞穴与奇形怪石。每年10月至翌年5月开放。<br />
<br />
尖峰石阵（The Pinnacles）<br />
　　在帕斯北方260公里处，为南邦国家公园（Nambung National Park）的一部分。这里有一片横跨沙漠的奇异活化石原始森林，再加上数以千计、甚至高达4公尺的石灰岩柱，蔚为奇观。欲游览本地，最好是搭乘全天巴士，或参加由帕斯出发的四轮驱动车（四驱车）旅游团。<br />
<br />
波浪岩（Wave Rock）<br />
　　位于帕斯以东340公里处，状似巨大波浪，拔高50米，耸立于天地间。帕斯每天均有旅游团前往此地，行程穿越达令山脉和壮丽的Jarrah Forests。</div>

							
							
							
							
