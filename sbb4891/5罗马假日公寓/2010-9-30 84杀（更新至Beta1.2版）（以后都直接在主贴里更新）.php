&nbsp; &nbsp;&nbsp; &nbsp; 人物选择上挑选了小组里比较著名的且个性鲜明的朋友，技能设定上以诸君的性格特点为基础，尽量保持了中立的态度，难免参入个人评判，但是确定：不包含任何道德层面的批判及任何人参公鸡的成分。仅博诸君一笑，莫要认真^_^。<br />
