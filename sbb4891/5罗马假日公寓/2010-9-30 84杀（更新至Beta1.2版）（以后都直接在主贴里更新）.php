基本牌：<br />
<br />
删帖：出牌阶段，攻击范围内对除自己以外的一名角色使用，效果是对该角色造成1点伤害。（同“杀”）<br />
<br />
翻墙：当受到“删帖”攻击时，可以使用一张“翻墙”来抵消“删帖”的效果。（同“闪”）<br />
<br />
法轮：法轮能在两种情况下使用： （同“桃”）<br />
&nbsp; &nbsp;1）在出牌阶段，可以使用它来回复1点体力。<br />
　　2）当有角色处于濒死状态时，可以对该角色使用“法轮”，防止该角色的死亡。<br />
<br />
锦囊牌：<br />
<br />
 喝茶：出牌阶段，对除自己外任意一名角色使用。由目标角色先开始，轮流打出一张“删帖”。“喝茶”对首先不出“删帖”的一方造成1点伤害，而另一方视为此伤害的来源。（同“决斗”）<br />
<br />
钓鱼：弃掉任意数量的手牌，并指定一名角色，该角色也要弃掉同样数量的手牌。（自创）<br />
<br />
真理部通知：出牌阶段，对除自己以外的所有角色使用。按行动顺序结算，除非目标角色打出一张<br />
”删帖“，否则该角色受到”真理部通知“对其造成的1点伤害。（同“南蛮入侵”）<br />
<br />
零八宪章：出牌阶段，对除自己以外的所有角色使用。按行动顺序结算，除非目标角色打出一张“翻墙”，否则该角色受到“零八宪章”对其造成的1点伤害。（同“万箭齐发”）<br />
