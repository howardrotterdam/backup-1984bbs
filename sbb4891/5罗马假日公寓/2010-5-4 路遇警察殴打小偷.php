出乎我意料的是，苗苗突然喊了出来：不要再打啦！不许再打人了！<br />
警察们暂时收起了警棍。<br />
一个警察有点尴尬，转身说：小姐，你问下，看这两个人是干嘛的！<br />
另一个警察说：不用理她。<br />
苗苗继续大喊：你们打人是不对的，你们是执法者，不能这么打人，你们是不对的。<br />
我试图拉住苗苗，但是她使劲挣脱，继续喊：网上这样的事太多了，我知道他们去了看守所，也是被你们暴打一顿。你们不能打人。<br />
最后我把苗苗拉走了。<br />
<br />
