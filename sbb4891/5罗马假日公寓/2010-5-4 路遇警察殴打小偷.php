<br />
整个事件，事情发生的地点是温州黄龙住宅区。没有夸张。我写得很平实，不符合一贯装逼的风格。因为这个事对我是个打击。我认清了自己虚伪的嘴脸。<br />
同时也是一个震撼。我跟苗苗经常吵架，吵得惊天动地，但是我一直觉得她无比真诚。<br />
不怕肉麻地说，这件事之后，我更爱这个女子。<br />
这是唯一值得庆幸的事情。<br />
