																											<div id="postmessage_709581" class="t_msgfont">国务院办公厅关于控制城镇房屋拆迁规模严格拆迁管理的通知》（国办发〔2004〕46) <br />
摘录<br />
所有拆迁项目都必须按照《城市房屋拆迁管理条例》（国令305号）和《城市房屋拆迁估价指导意见》（建住房[2003]234号）等规定的权限和程序履行职责，严格执行申请房屋拆迁许可、公示、评估、订立协议等程序；对达不成协议的，必须按照《城市房屋拆迁行政裁决工作规程》（建住房[2003]252号）的规定严格执行听证、行政裁决、证据保全等程序。特别要执行拆迁估价结果公示制度，依照有关规定实施行政裁决听证和行政强制拆迁听证制度，确保拆迁公开、公正、公平。<br />
地方政府不得违反法定程序和法律规定，以政府会议纪要或文件代替法规确定的拆迁许可要件及规划变更，擅自扩大拆迁规模。<br />
拆迁人及相关单位要严格执行有关法律法规和规定，严禁野蛮拆迁、违规拆迁，严禁采取停水、停电、停气、停暖、阻断交通等手段，强迫被拆迁居民搬迁。<br />
<br />
拆迁公告缺少内容<br />
拆迁公告一般内容写着建设项目名称、拆迁人、拆迁实施单位、拆迁安置补偿标准、拆迁期限、拆迁范围、立项和立项文号,规划和规划文号,土地定点和土地批准文号。你可去对照一下公告是否缺少这些要素,特别是缺少拆迁许可证五要素中的三要素:立项,规划,土地批准书。缺的话你就要小心了。拆迁公告的日子和有效期你也得看清楚,过了有效期还没拆的话,他必须去补办。<br />
<br />
（一）建设项目批准文件；一般由地方计划委员会批准立项。（二）建设用地规划许可证（附红线图）；一般由地方城市规划行政主管部门核发。（三）国有土地使用权批准文件；一般由地方土地行政主管部门颁发。（（四）拆迁计划和拆迁方案；是指拆迁人对将来准备实施的拆迁活动所制定的计划和方案。（五）办理存款业务的金融机构出具的拆迁补偿安置资金证明；由拆迁人开设房屋拆迁补偿安置资金专用帐户，一般由拆迁人、金融机构、房屋拆迁管理部门三方监管。<br />
<br />
拆迁补偿比他协议上的高得多，不要被忽悠了<br />
<br />
上你们那国土资源部的网站，你们应该有土地证吧，没把你们的证注销，不可能发的出拆迁许可证，要注意他们混水摸鱼，偷偷注销你的土地证<br />
站内PM我，被拆了两次了，这个天杀的拆迁制度<br />
<br />
自相矛盾的拆迁程序设计<br />
在拆迁活动中，既然政府出卖土地，政府就应该先征用公民的私有财产，征用了，拆迁完了，再把土地交给开发商进行土地开发。既然城府卖土地，政府应该是拆迁人才对。可是这么多年来，拆迁人的地位一直就是开发商。那就将错就错，姑且认为法律上规定的就是对的吧。<br />
我们再来看看拆迁的程序设计中有没有什么问题。<br />
要进行拆迁，其程序首先是要申请拆迁许可证。《拆迁管理条例》第六条规定：“拆迁房屋的单位取得房屋拆迁许可证后，方可实施拆迁。”第七条规定：“申请领取房屋拆迁许可证的，应当向房屋所在地的市、县人民政府房屋拆迁管理部门提交下列资料一）建设项目批准文件；（二）建设用地规划许可证；（三）国有土地使用权批准文件；（四）拆迁计划和拆迁方案；五）办理存款业务的金融机构出具的拆迁补偿安置资金证明。市、县人民政府房屋拆迁管理部门应当自收到申请之日起30日内，对申请事项进行审查；经审查，对符合条件的，颁发房屋拆迁许可证。”<br />
根据上述规定，政府在颁发“拆迁许可证”的时候，开发商的项目就已经获得了“建设项目批准文件”、“建设用地规划许可证”、“国有土地使用权批准文件”等房地产项目开发所必须的各项行政许可。开发商具备了这几个条件后，政府准予拆迁，是“只欠东风”的事儿；不准予开发商拆迁，几乎不可能发生。<br />
紧接着，政府很自然地准予拆迁、颁发“拆迁许可证”。颁发了“拆迁许可证”，就等于政府认同了开发商提交的“拆迁计划和拆迁方案”。这个时候，究竟如何拆迁、如何补偿，被拆迁的住户尚不知晓。为什么，因为只有准予拆迁，政府才会公告拆迁事项。《条例》第八条规定：“房屋拆迁管理部门在发放房屋拆迁许可证的同时，应当将房屋拆迁许可证中载明的拆迁人、拆迁范围、拆迁期限等事项，以房屋拆迁公告的形式予以公布。”<br />
问题来了。我的土地、我的房屋，我手里头可还有《房屋所有权证》和《土地使用权证》呢，政府怎么说批准拆迁就拆迁呢？拆迁方案经过我同意了吗？征求我的意见了吗？按照全国人大通过的《城市房地产管理法》和《土地管理法》的规定，持有《房屋所有权证》、《土地使用权证》的，他当然就是土地的使用权人、房屋的所有权人，其物权就应获得国家保护。现在怎么却一物二主、一女两嫁了呢？<br />
铁的现实是：尽管你手里还持有着有效的《土地证使用权证》、《房屋所有权证》，上面还印着金灿灿的国徽，但无论如何，这也是低档不过《拆迁许可证》的威力的。<br />
如此的拆迁制度告诉我们：政府不仅可以出卖了你还所拥有的使用权的土地，同时，就开发商的拆迁拆迁方案（补偿方案），政府也有权替你向开发商作出同意的决定，而无论你是否知道和愿意。<br />
DD这是什么样的程序设计！？世界上还有哪个国家会有这样的拆迁程序？！<br />
