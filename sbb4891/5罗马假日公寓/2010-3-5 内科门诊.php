　　　　　　　　　　　　　　　张官云 <br />
<br />
　　这是我的亲身经历，绝无虚言。<br />
<br />
　　九十年代，我从云南来到了湖北武汉念书。云南的大多数地方和昆明一样，四季如春，勐海和普洱也差不多。没想到武汉的气候完全不是那么回事，不仅四季分明，而且根本没有暖气和空调，就连电风扇都没有！所以，云南人最难适应的地方就是这里，我当年就得了鼻炎。<br />
<br />
　　鼻炎很难治，鼻炎平、鼻炎康、鼻舒适、鹤胆丸......我什么药都吃过来了，就是治不好，不但治不好，还把肠胃给吃坏了。从此又增加了一个慢性肠炎，苦上加苦。其实很多鼻炎患者都有并发症。<br />
<br />
　　慢性肠炎的最大特点就是爱拉肚子，吃辣了、喝酒了、吃苦了......随便一个原因，或者没有原因，都会拉肚子。<br />
<br />
　　我回到昆明后，鼻炎治好了，但是肠炎可不会好。有时候很争气，更多的时候，某种零食都会让你捂着肚子到处找厕所，狼狈不堪，哈哈。慢性肠炎的人，更大的烦恼是口腔溃疡，如果你连着几天拉肚子，那么口腔溃疡在所难免，就算没有破口，也会不小心咬破，然后就感染了。接着溃疡变大，疼痛不已！<br />
<br />
　　后来我才知道，中医认为，脾胃失调，就会导致口腔溃疡。而西医则认为，肠胃中的有益菌在生产着维生素B，一旦这些有益菌落被破坏，体内就缺少维生素B，于是就会口腔溃疡。甚至神经系统也会失调，导致牙齿和口腔打架，于是就可能咬破口腔，进一步加剧溃疡......真是恼人啊！<br />
<br />
　　我从小就喝茶，在勐海长大、在普洱上的中学，这样的条件，当然就喝本地茶：普洱茶、绿茶、也有红茶，我比较喜欢喝生普洱茶，但是这个偏好对我很不好。生普洱茶本来很好，只要你肠胃好，就没问题，但是它太寒，肠胃不好的人受不了，而我的茶瘾又特别大，喝茶总是很浓，这样就更加伤害肠胃。我家是茶叶世家，我父母是搞茶叶科研的，我们对于不清楚的东西，总是抱着谨慎态度。而那个时候，普洱茶的熟茶，成分很不清楚，到现在，也只知道很小一部分，所以我们一般就喝生茶。<br />
<br />
<br />
　　直到我请教了昆明植物所的陈可可老师，这个问题才得到了答案。陈可可老师是搞微生物研究的。这几年，他在普洱茶的微生物方面也作了很深入的研究，并且成功地分离和接种出20多种微生物。经他介绍，我对普洱茶的成分有了更加科学地认识，才知道普洱茶的多种微生物都没有害处，也更加放心了。<br />
<br />
　　从此，我就改为喝熟茶，我发现，我的慢性肠炎，奇迹般地好转了，很少犯病。伴随着肠炎的痊愈，我的营养不良也跟着消失，身体素质得到很大的提高。上个星期，我还组织了一次骑单车环游滇池，整整140公里，我们12个小时就跑完了；最让我高兴的是，口腔溃疡似乎成为了历史，原来那种每个月至少一次的痛苦，不再出现。<br />
<br />
　　中医的解释是：生普洱茶性凉，但熟普洱茶性温，所以暖肠胃。西医的解释比较复杂：人的肠胃里有很多有益细菌，他们与人类共生。如果菌落不平衡，就会生病。而熟普洱茶中有大量的有益菌，可以帮助肠胃重新平衡，这样，病就好了。不管是哪一种解释，都说明熟普洱茶对人的肠胃很好。<br />
<br />
　　真是一方水土养一方人，普洱茶就象一个很高明的保健医生，不同的人都会从它那里得到不同的好处，我不仅从事普洱茶，而且从内心热爱这种饮料，她是伟大的、自然与文化的双重遗产！</div>

							
							
							
							
