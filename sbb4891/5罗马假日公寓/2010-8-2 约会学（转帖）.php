由于本书将要连载一段比较长的时间，在这里先做一个框架导读。 <br />
　　 <br />
首先想说。真的高手，必定大隐隐于朝（官员，教授，导演们）。约会达人在生活中无处不在，我们这些教人约会的，要说是顶尖水平，那真是贻笑大方了。我们只是愿意分享自己的经验而已。 <br />
　　 <br />
常言道，有心栽花花不开，无心插柳柳成荫。约会这个事儿，无论是谈感情寻真爱，还是打发时间找刺激。最发人深省，最具教育意义和操作细节的作品，压根就不是所谓的“约会导师”整出来的。 <br />
　　 <br />
对于感情。我推荐全世界最畅销的童话：《小王子》。看它用不了40分钟的时间。无论你看过多少便了，我建议你换个姿势，再来一次。我向来不吝以最大的热情推荐此书，把它说成是上泡学论坛，以及看我这本《约会学》之前的必读物。 <br />
　　 <br />
酒肉穿肠过，佛祖心中留。在约会中，如果说技巧和手段乃是不入佛门禁地的酒肉外道，那“感情”，必定是佛祖正宗。多读几遍《小王子》，不会在歧途上走火入魔。 <br />
　　 <br />
附中国历史上最著名的约会达人之一――六世da赖的情诗一首 <br />
　　 <br />
　　【你见，或者不见我 <br />
　　 我就在那里 <br />
　　 不悲不喜 <br />
　　 <br />
　　 你念，或者不念我 <br />
　　 情就在那里 <br />
　　 不来不去 <br />
　　 <br />
　　 你爱，或者不爱我 <br />
　　 爱就在那里 <br />
　　 不增不减 <br />
　　 <br />
　　 你跟，或者不跟我 <br />
　　 我的手就在你手里 <br />
　　 不舍不弃 <br />
　　 <br />
　　 来我的怀里 <br />
　　 或者 <br />
　　 让我住进你的心里 <br />
　　 默然相爱 <br />
　　 寂静欢喜】 <br />
　　 <br />
再说找刺激，它和感情无关。对这部分内容，我推荐天涯的一个帖子《不说JP，说说我认识的花花男人》。地址是：<a href="http://cache.tianya.cn/pub/c/no11/1/417277.3115.shtml" target="_blank">http://cache.tianya.cn/pub/c/no11/1/417277.3115.shtml</a> <br />
　　 <br />
贴主是个mm。帖子里面的各号男人没一个是真正谈感情的。但这并不影响他们本着和mm双赢的态度，随时解决性生活的问题。这一篇帖子，胜过当今网络上所有的《花花公子教程》。 <br />
　　 <br />
说完“无心抽柳”，可以正式介绍“有心栽花”的约会学框架了。 <br />
<br />
我们在生活中经常能遇到这样一种模式：我要和一个特定目标进行交互行动，来达到某个目的。 <br />
<br />
销售。我要把产品卖给消费者。“消费者”是特定目标，“卖出产品赚到钱”，是我的目的。 <br />
战斗。我要打败敌军。“敌军”是特定目标，“打败它”，是我的目的。 <br />
政治联合。我要联合某个政治势力来巩固我地位。“某个政治势力”是特定目标，“联合它以便巩固我的地位”，是我的目的。 <br />
约会。我要和一个女人幸福的生活在一起。“某个女人”是特定目标，“和她幸福的生活在一起”，是我的目的。 <br />
<br />
要想解决这一类问题。有一个基本的4+1体系。 <br />
<br />
首先是四项准备工作。 <br />
<br />
1. 了解“特定目标”。 <br />
他的生活习惯是什么？ <br />
他的处事风格是怎样的？ <br />
他有哪些人性上的弱点可以被利用？ <br />
<br />
2.了解我们自己。 <br />
我有哪些地方是“特定目标”所喜欢的。 <br />
有哪些地方是他所讨厌的。 <br />
<br />
3. 了解我和这个“特定目标”之间有可能存在的各种关系。 <br />
相互有好感。 <br />
相互讨厌。 <br />
深厚纯洁的感情。 <br />
纯金钱利益关系。 <br />
等等等等。 <br />
<br />
4.了解各种沟通手段。即我采取怎样行为，可以对“特定目标”产生怎样影响。 <br />
我骂他，会让他生气。 <br />
我完全不甩他，会让他不服气。 <br />
我处处奉承他，会让他开心，但是他不一定看得起我。 <br />
我私下不甩他，公众场合奉承他。他会知道我是在给他面子，因而感激我。 <br />
我不动声色暗中帮助他，他当时不一定知道，某天知道了，他会感激我。 <br />
等等等等。 <br />
<br />
上面四点是相互独立的。掌握它们，只是做到了“知己知彼”，真正的大头，还在于有效的“全盘操作模式”。要想“百战百胜”，“战”才是最重要的！ <br />
<br />
什么叫“全盘操作模式”？ <br />
<br />
比如我要搞掉一个政治对手。即时我掌握了上述4点，我一开始就根据他所讨厌的东西处处争锋相对，这依然是盘臭棋，两败俱伤。聪明人往往会利用自己所掌握的4点基本知识，投其所好，拉拢，怀柔，联合他。然后抓住一个破绽，出其不意，狠狠的一击必杀。 <br />
<br />
又比如，我要追求一个mm。我掌握了上述4点，是否就要不遗余力的投其所好呢？假设这个女人爱钱，恰好我也有钱，我就该狠狠的用金砖砸她吗？结果往往是她花着我的钱和别的男人厮混。高手往往会对这样的女人应该表现出如下态度：“我很有钱。假如我爱谁，我会毫不犹豫的用金砖砸死她！但是我现在一个子儿都不会花在你身上。因为，我还没看出来你有哪点值得我爱的。你必须争取我的爱！” 然后，在该mm“争取”的时候，高手又会适时的放下身段，让她的努力“卓有成效”，而不会始终保持框架，逼得mm多次碰壁，只能放弃。 <br />
<br />
这就是4+1：四项基本准备工作，加上一个有效的全盘操作模式。 <br />
<br />
我这本书的前五章，就分别讲述了这五点。 <br />
第一章谈正确认识女人以及判断方法――如何不被其表象所蒙蔽。（了解女人） <br />
第二章谈各阶段，各类型的男女关系。（了解和女人之间可能存在的各种关系） <br />
第三章谈各种男性魅力（小到衣着细节，大到性格心态）以及快速练习提高方法。（了解自己） <br />
第四章谈如何通过语言，肢体，眼神，精神控制等与女性沟通交往，并对其产生各类影响。（了解各种沟通手段） <br />
第五章讲授情场高手在通常情况下的全套情场操作模式，外加一些特殊的情感操控模式。 <br />
（第六，七章保密） <br />
<br />
<br />
有了上述的框架，我们再来看看现存的各种约会学经验所存在不足。 <br />
<br />
目前看来，只有《谜男方法》一本书包括了上面5大内容。 <br />
<br />
David Deangelo所推崇的“风趣自大”，有没有用？有！但这仅仅是上述框架的第二点（了解自己，风趣自大是男性自有魅力之一）和第四点（了解沟通手段，风趣自大可以在沟通中让女性对你产生兴趣）。David对于最重要的“全盘操作模式”，几乎毫无提及。后来仿造谜男方法拼凑出一套模式来，骗骗没有实战经验的人还可以，我是看都看不下去。 <br />
<br />
Swingcat提出的“让女人把自己当做奖品”。仅仅是“全盘操作模式”中间的一部分。至于4点准备工作，他提得甚少。 <br />
<br />
Ross Jefferies倡导的“NLP诱惑学”，则是准备工作中的第四点，沟通手段之一。对于其它内容，他也没有涉及。 <br />
<br />
还有一些网贴中提及的各种男性魅力：气量，风度，果断，责任心，事业心，爱心，能力，收入等等等等。都属于准备工作的第二条：男性的自有魅力（了解自己）。这些条件做好了，固然有魅力。但你能说自己是这个世界上做得最好的吗？你所追求的女人，她就不认识比你更优秀的男人了？倘若真是如此，那这个女人混得也太次了，你追她没什么意思啊。假如她还认识比你更优秀的，你凭什么让她喜欢的是你，而不是那个人？！ <br />
<br />
所以，绝大多数的约会学教程都不全面。至于国内的各位约会学导师。大家可以根据上述体系自行帮他们归类。立刻就能发现他们的侧重点在哪里，不足又在哪里。 <br />
<br />
现在讨论另一个重要问题：这么有心计的去约会，值不值？该不该？泳湿小狗莫非是一个不折不扣的“权谋派”？ <br />
<br />
这要分两部分来谈。 <br />
<br />
第一，作为一个理性的人，不要轻易被感性的观点所影响。 <br />
<br />
有一位id是“良辰美景2008”的mm批评我：“你太强调技巧了.舍本求末的典型.太苦心追求一件事,比如追女人,这个态度本身就累,做人就该顺其自然,太辛苦太算计才得到东西,没劲.” <br />
<br />
mm的这句话说得很感性。累，没劲，鄙视意味甚浓。一个真男人，洒脱快乐，何至于这样苦心追女人！ <br />
<br />
但我立刻可以搬出另一段更感性，更具煽动性的话来：“你现在还小，不懂。但是这个很重要，非常重要。你想，等你到了我这个岁数，你没准也会问自己，从小到大，这辈子，有没有遇见过那样一个姑娘，那脸蛋儿，那身段儿，那股劲儿，让你一定要硬，一定要上？之后，哪怕小二被人剁了，镟成片儿，哪怕进局子，哪怕蹲号子。这样的姑娘，才是你的绝代尤物。这街面上，一千个人里只有一个人会问这个问题，一千个问这个问题的人只有一个有肯定的答案，一千个有肯定答案的人只有一个最后干成了。这一个最后干成了的人，干完之后忽然觉得真他妈的没劲儿。但是你一定要努力去找，去干，这就是志气，就是理想，这就是牛逼。”――冯唐，《十八岁给我一个姑娘》 <br />
<br />
冯唐这段话也是在鄙视。成天傻乐自嗨，算男人吗？被不爱的人簇拥着又如何？弱水三千，我只要那一瓢。真男人就该看准了一个目标，想尽各种办法百折不挠的把它拿下！ <br />
<br />
所以说。价值观这个东西。往往容易被文笔好的人随意强奸。这么看也有道理，那样看也是对的。这就很扯淡了。你想要什么，你自己心里清楚，不能轻易被人影响。 <br />
<br />
第二：约会学的本质真的是“权谋”？难道“约会”不是一件让人快乐的事情吗？ <br />
<br />
咱中国人聪明，特别喜欢务虚。无论啥领域，搞销售的，搞艺术的，搞企业的，搞武术的，总爱讨论“道”与“术”孰轻孰重之类的高深问题，往往争得不可开交。 <br />
<br />
就说做企业。管理界光是“西学为体，中学为用”还是“中学为体，西学为用”就争了好多年。作为一个喜欢到处凑热闹的人，我至少在5个不同的现场听到长江商学院院长项兵谈论做企业的“道，法，术”。据说项兵的出场费是15万，敢情就讲这么一个论题，光我在场的时候，丫就赚了75万，总时间还不到5个小时。他的“为商之道”有多么牛逼，我不是很清楚，但我知道他的“赚钱之术”很牛逼。 <br />
<br />
我总想，哪天我扫大街去了，手持长帚，背对夕阳，是不是也该思考一下手起帚落的“道”与“术”呢？ <br />
<br />
这一年约会学蓬勃发展，很多朋友就开始讨论：什么是真正的约会达人？成为一个泡妞高手的人生意义何在？约会要想成功，究竟是靠手段技巧，还是靠心态和实力，抑或就靠那一颗炙热的心？ <br />
<br />
对于这些讨论，我的头很大很大。为了显示我不是那么肤浅。我也拉个大旗出来谈谈：明朝大儒王阳明。 <br />
<br />
高中物理课上，我从一本近代史小说里看到了一个很给中国人长脸的句子：“一生俯首拜阳明”。 <br />
<br />
说这话的人叫东乡平八郎。他是日本的海军元帅，也是日俄战争（在中国东北打的，操他大爷的！）期间日本的联合舰队指挥官。丫把这句话刻成小木牌，到死都挂在身上。 <br />
<br />
