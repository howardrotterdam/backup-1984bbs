<br />
过了一个多月，兄弟在社团活动中碰到了这位美女。搭讪说话啥的都挺正常，qq号也拿到手了。可我兄弟“打压”了一句：“你这种漂亮又会打扮的女孩子，屋里肯定乱糟糟。哪有时间收拾啊。”那丫头立马变了脸色，没说几句就走了。兄弟哭丧脸回来，加qq不给通过。兄弟跟我说，“打压”也是你教的，这“优势”咱也用上了。结果黄了。 <br />
<br />
我说你就是个SB！你这怎么说话的？你可以跟她说：“我每天累得要死。又要组织社团又要学习考试的。你别看我现在西装革履像模像样的，我屋子里乱得不行！真佩服你们女孩子还有力气收拾屋子” 这样让她产生共鸣，她可能会说：“哎呀，我也是这样的！屋子里特乱。”就算没共鸣，也不会产生负面影响。打压根本就不是这种情况下用的！ <br />
<br />
兄弟大濉 <br />
<br />
这就是典型的“认识”和“操作”的差距。本书前四章都是谈认识，带五章才开始逐渐进入操作阶段。朋友们一定要注意，不要在自以为开窍后立刻实践。不懂得好的操作方法，就算你会通灵术也不成。 <br />
<br />
3. 请保持耐心。 <br />
<br />
本书前面的三四个章节，有一定基础的朋友们都了解，可能你们觉得是废话。请不要匆匆忙忙抡砖开砸。泡上一壶好茶，慢慢看，提点意见，不是挺好吗？好戏在后头。 <br />
<br />
序言就写到这里， 由于个人水平和研究经费的限制，本书不足之处在所难免，还请批评指正。</div>

							
							
							
							
