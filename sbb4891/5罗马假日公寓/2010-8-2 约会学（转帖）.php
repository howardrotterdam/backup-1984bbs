我问自己，假如是现在的我，初次认识赵敏，能泡到手吗？答案如下：对于大一时候的赵敏，完全没问题，很轻松。对于现在有这么一个男朋友的赵敏。就会比较困难。但是背着她男友搞一夜情大概是有机会的。赵敏也不是省油的灯，呵呵。 <br />
<br />
现在回想和赵敏之间的事情，我认为她最初确实有一点喜欢我。原因大概是我本人条件不错，同时在和她认识之初，并没有像其他拙男一样，立刻春心大动，拜倒在她的石榴裙下。直到修电脑那天，我彻底动心了，她也就对我失去了兴趣。 <br />
<br />
至于最初和赵敏的那段成天腻在一起的日子，她所有的课余时间，精力，关注点都花在了我一个男人身上，而她的心，却只分给了我一点点。很多人可能会觉得这不正常。多年之后，我也成了一个约会高手，我发现这太他妈的正常了。这就是所谓的“用真心泡妞”的初级阶段。Mm们都以为我是全身心的投入，看不出丝毫破绽。 <br />
<br />
行了。这个纠结的故事讲到这里也差不多了。许多反感约会学的mm看了这个故事，可能会拿赵敏的现任优秀男友来鄙视约会学的“卑劣”。这里我想说，假如赵敏的男友真如我所看到的那么优质，这就正好从女性的角度证明了“约会学”的作用：只有赵敏这种高手才能把他拿下。周芷若客观条件不比赵敏差，假如她也对这个男人动心，她是争不过赵敏的。倘若反过来，是这个男人对周芷若动心了，那么他也铁定争不过我。 <br />
<br />
虽然我从来不认为“约会学”很阴暗。不过，如果有人一定要说它阴暗，我不妨顺着他们的话来说：约会学就是把自己训练得阴暗一些，然后去俘获真正的白马王子或者白雪公主。这不挺好的吗？现实生活不是言情小说，阴暗的人太多了，白马王子和白雪公主极少会自然的，毫无阻碍的结合到一起。《傲慢与偏见》只是小说而已，要想成功，有时不得不比别人更阴暗。当然了，两个阴暗的人，都知道对方是什么货色，也难以结合，不过一夜情往往发生在他们之间：） <br />
<br />
更退一步说。菜根谭曰：“势利纷华 , 不近者为洁 , 近之而不染者尤洁 ; 机械智巧 , 不知者为高 , 知之而不用者尤高。”所以，想要做到最高层次，你也必须掌握约会学的知识。只不过，“知之而不用之”而已。 <br />
<br />
不扯太多了，还是继续讲我自己的东西吧：） <br />
<br />
这个故事反应了很多东西，我以后也会一一引用。在此，我仅仅想强调修电脑的那一幕。假如当时的我深刻理解了“上帝面前大家都是普通人”这条常识。我就不会相信赵敏的那段话。管你再美若天仙，你也是普通人。除非是彻底的同性恋，谁在青春年少时没有过春情萌动的时刻？无非是程度深浅而已。 <br />
<br />
但是赵敏做得太像了。同时她又是美女，她那段话貌似也符合逻辑：【从来都是别人追我】。所以，我在不知不觉中就矮了三分。如果是今天的我，首先就不会在时机未到的情况下讲小昭故事，就算讲了，面对赵敏那样的反应，我只会不屑的回应她：“扯吧你就！” 然后该干嘛干嘛。 <br />
<br />
很多兄弟之所以容易被美女权贵们打压和控制，往往是心中有所求。面对美女，想要上她，面对高官，总想着以后可能会有求于他。自然就底气不足。 <br />
<br />
“再次路过之”和“孔二狗”的帖子里都说到了这一点。这里转帖一下。 <br />
<br />
再次路过之说： <br />
<br />
【色即是空，空即是色！保持平常心非常重要，简单说来，你长得再漂亮我只要不想和你上床就无所谓， <br />
你再有钱我不图你的钱也无所谓，无欲则无求，无求则不受制于人，才能挺起腰板面对自己的目标。】 <br />
<br />
《黑道风云二十年》里，赵红兵和沈公子则有如下的对话 <br />
<br />
【“人家冯检是个副地级干部，我真纳闷儿，你就没一句正经的怎么就把人请来了。” <br />
“副地级干部怎么了？副地级干部不是人啊，检察长就不是人啊？说不定人家比我流氓多了。再说，你把他当副地级干部，我可没把他当过。当年，咱们开亚运饭店时，冯检就是个研究生毕业没几年的小伙儿，没少在咱们饭店赊账，你不管钱当然不知道，我要是把以前开饭店那堆欠条儿找出来，说不定上面还有他签的条子。” <br />
<br />
“那人家现在是检察长了，身份不一样了，你就不能那样跟人家说话了。” <br />
“红兵，问你件事儿呗！” <br />
“说！” <br />
“咱刚复员那会儿，你爸的官儿比冯检大吗？” <br />
<br />
“权力大一点点吧，级别一样的。” <br />
“好，就算是一样，那我问你一句。为什么你爸除了我骑摩托太快以外从来没批评过我，但一见到小纪就习惯性的抬腿就踢，四儿啊什么的，一见你爸就哆嗦，这是为什么。” <br />
“小纪、四儿，他们几个从小我爸就认识，从小收拾他们收拾习惯了。你不同，我爸认识你的时候，你至少23了。” <br />
“21.” <br />
<br />
“就算21，那也不小了，我爸那是不好意思训你。” <br />
“扯。” <br />
“那你说是为什么？” <br />
“因为，我从来就没怕过你爸，从来就没因为你爸的地位对他有什么畏惧。我和他聊天的时候，我和他地位平等，我把他当成朋友和他聊天。你爸爸在我眼中，不是市委常委，只是个和我比较谈的来而且懂得比较多慈祥的老人。久而久之，你爸爸也把我当朋友了。小纪他们一见到你爸爸就是一副要挨踢的熊样儿，换了我，我也踢他。”】 <br />
<br />
类似段落。在别的文学作品里面也屡见不鲜，我从小到大看得多了。孔二狗的帖子，都快两千万点击率了，那么多人看过这一段，但我相信真正深有感触的人未必很多。说起来谁都能头头是道，做着就不是那么回事儿。我是经历了赵敏的事情，才有了彻底的转变。所以上面费笔墨讲了这么长一篇故事，不是为了凑篇幅。假如直接引用路过之和孔二狗的文章。我这《约会学》也没啥教育意义了。</div>

							
							
							
							
