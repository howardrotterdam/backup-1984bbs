<br />
王阳明是个怎样的牛逼人物呢？简单说，他既是著名的唯心主义哲学家，又是一个杰出的军事家。 <br />
<br />
王独创“心学”，根据百度百科介绍：【王守仁以心(良知)立言，又以良知释心．心(良知)就构成了王学的基石。在王守仁看来，心是无所不包的。物、事、理、义、善、学等都不在“吾心”之外，亦即是“心即理”。】 <br />
<br />
王以观花为例对“心学”作了很形象的描绘：【先生游南镇，一友指岩中花树问曰：天下无心外之物，如此花树，在深山中自开自落，于我心亦何相关? 先生曰：你未看此花时，此花与汝心同归于寂，你来看此花时，则此花颜色一时明白起来，便如此花不在你的心外。】 <br />
<br />
而作为一个“唯心”的哲人，王带兵打仗，一辈子指哪打哪，百战百胜。 <br />
<br />
没有人相信，王阳明在征战沙场的时候会用到任何“心学”理论。【你没看到敌军时，敌军与汝心同归于寂，你看到敌军时，敌军数量一时明白起来，便如此敌军不在你的心外。】这他妈不是扯淡吗？这能打胜仗吗？！ <br />
<br />
同时也没有谁认为王的“心学”是凭空编造出来忽悠人的。这是他在经历了仕途沉浮，“龙场悟道”之后，对人生的一种全新认识。一个虚构的哲学观点，不会把精明的日本人忽悠这么多年。 <br />
<br />
王阳明的人生观（道）很牛逼。他在生活中带兵做事（术）也很牛逼。他的道和术之间，不仅不统一，相反有矛盾。但这无关紧要。王阳明剽悍的人生不需要解释！丫生前作为一个杰出的将领，保家卫国，享受了高官厚禄，皇帝赐封。挂了以后，作为一个哲学家流芳百世。中国历史上能做到这样的独此一个。 <br />
<br />
王阳明让人性中的矛盾共存，这正是王所提倡的“知行合一”的更深一层涵义。 <br />
<br />
回到约会学。什么是真正的约会达人？技巧为王还是感情制胜？我们如何看待“约会”，如何看待女人？ <br />
<br />
就我个人来说，我的主体意识（道），是尊重女人，爱护女人的。女人是男人的肋骨，我们相亲相爱，才能让人类社会生生不息。但我的细节操作（术），则必然符合相应的社交学手段，我不会因为尊重爱护女人而去宠溺她们，让我显得卑微。有的时候，我甚至把女人当作调教的对象。 <br />
<br />
“道”和“术”相互矛盾。这没啥大不了的。约会作为一场温暖的游戏，它能为我们带来纯洁的爱情，很多时候又需要斗智斗勇。 <br />
<br />
我不认同在男人们在约会中应该全方位的随心而至，享受过程。这样成功率会很低 <br />
我也不认同权谋派的说法，觉得约会达人就是应该用各种手段把女人玩弄于股掌之上。这样就失去了本质的快乐。 <br />
<br />
但是，认不认同哪种说法都没太大关系。最无聊最浪费时间的，是参与不同观点之间的争论，并试图让它们辩证统一。有这个争论的工夫，还不如约个mm出去喝杯咖啡呢！ <br />
<br />
我这本《约会学》，它也许能帮你找到女朋友，也许仅仅是你打发无聊时间的一些琐碎文字，或者你干脆就觉得它是一本变相的“权谋秘籍”，这都没关系！不管哪种情况，你没必要花很多时间去讨论一些形而上的问题。聪明的你，必定知道我的意思：）</div>

							
							
							
							
