该干部和我家关系特好，他妈妈从小就和我外婆是街坊，他和我舅舅在年轻时候是当地混混的两大头目，特调皮，我外公以前没少收拾他。 <br />
　　 <br />
我和他有9年没见了，这些年我一直在外面读书，和父母的朋友接触得也不多。9年前的我还是个小小的拙男，这个叔叔已经是相当牛逼烘烘的某省厅长了，还是一个强力部门。我从小就觉得他家闲杂人等特多，都喜欢干三件事：拎包，付账，拍马屁。他则是派头十足，气场极其强大。我总觉得和他之间距离太远。虽然父母和他关系特好，但我打记事起，直到九年前最后一次见到他，也没和他说上过几句话。总是恭恭敬敬的叫叔叔。不敢多说什么。 <br />
　　 <br />
那天在洗浴中心。我妈妈和同去的女士一起先进女宾部了。我和他两人留在大厅的沙发上等人。我两人的沙发呈90度角。 <br />
　　 <br />
我有点拘谨，低着头。他则很轻松的靠在沙发背上面看手机短信，完全不盼摇Ｎ野抵泄鄄焖，发现他的肢体语言果然很符合约会学中间“男性领袖”的一些特征：放松，自然，身体后仰，充分占据空间，又不显得精神萎靡。观察了一会，我觉得自己必须打破这种沉默的气氛，和他进行有效交流。因为我脖子实在太酸了，我他妈的想抬头。 <br />
　　 <br />
于是我也轻轻的靠在沙发背上，放松姿态。然后，温柔的平视他，就像在看自己的孩子一样。我喊了一声：“x叔啊” <br />
他看着我，挺俏皮的扬了扬下巴，示意我说话。 <br />
我说：“我听说你现在舒服了，管的这个XX中心相当不错啊，级别是副部，位子比副部长还舒坦，相当于土皇帝。” <br />
他笑了：“还不是一堆麻烦事。” <br />
　我也憨憨的笑着，但是肢体语言没有改变。依然是轻松的靠在沙发上，用温柔的眼神平视他，没有说话。 <br />
他调整了一下姿势，靠向我这边的沙发扶手上，轻声说：“我不行咯，副部不给我做。我就来这里当个头。我直接就这么跟x部长说的。不过我这里条件确实好。是12个副部级中心里面，我手里的地是最多的。” <br />
我说：“地最多啊？业务方面呢？” <br />
他说：“业务就那样，也没什么任务。这个地我可以拿来开发，打算也做个这样的洗浴中心，再搞个写字楼。各个省里面，我们的地也不少，在澳洲和新西兰那边也有几千亩地，看看打算怎么开发吧。” <br />
我乐了：“现在都不务正业啊。全搞开发了。” <br />
他笑着不说话。 <br />
我问：“冉冉呢？打算怎么安排？（冉冉是化名，他的女儿，和我差不多大，不学无术）” <br />
他说：“打算先安排在下面的省里做吧，在慢慢折腾到北京来……” <br />
　　 <br />
我把食指放在嘴上示意他小声，看着他，把身子靠过去。他也凑过来。我说：“小声点儿好，你说这些得注意，人听到了就能放网上去。” <br />
他就一直保持身体前倾，手撑在腿上，很认真的对我说：“我仔细想过，冉冉文凭不行。考公务员肯定也考不上。现在北京都很严，我也没办法的。唯一的路子就是先给她在地方找个位子做着。一两年后再慢慢调上来。。。” <br />
　　 <br />
我慢慢的恢复了靠在椅背上的姿势，只保持了温柔的眼神直视。他还在那儿说着冉冉的事情，我却没有听进去。我只是觉得很神奇，原来眼神，语速和肢体语言的效果真的有这么强大，不仅仅是对女人。我和他九年没见了，就算他和我父母关系再好，让一个比我大几十岁，级别还挺高的男人这样对我袒露心扉，也并不简单。 <br />
　　 <br />
半小时后我们等的人来了，他还意犹未尽，想让我在冉冉和土地开发的事情上给他点建议。他说年轻人思路广，脑子活泛。不过谈话不得不结束了，在别人面前他不想说这些。 <br />
　　 <br />
上面两案例是关于初次见面的眼神交流。具体操作，我在第四章讲。这里只是说给兄弟们体会一下。我自己重新看，都觉得读者可能摸不着头脑，不知道我在表达什么。不好意思了，文笔太差，只适合写教材，不适合写故事。 <br />
　　 <br />
还有，请注意我言谈之中所表现的态度：不卑不亢，不拘谨也不讨好，带有一点点母爱般的关怀和温柔。所有牛逼的气场在“母爱”面前都毫无作用可言。男人女人都扛不住。 <br />
　　 <br />
二。 必须以一个正常人的姿态打压强者气场。 <br />
　　 <br />
什么是正常人？你在顶头上司面前，必须保持谦卑，必须拍马屁。这叫正常。打压领导的气场，不能打破这种正常的局面，否则得不偿失。再比如，一个容貌气质极佳的美女出现在无所事事的你面前。这种情况下，张艺谋可能会无动于衷，丫见得太多了。但我们不是张艺谋，如果我们无动于衷，跟没看见似的，那行为就相当的做作。打压她的气场是可以的，但前提是，必须保持一个男人见到美女时应该有的反应，这才叫正常。 <br />
　　 <br />
案例三。 <br />
　　 <br />
也是这次回国。在武汉参加某常务副厅长的饭局。在座的有几个地方上的局长，把厅长左右的几个位置站满了。过程不用我多说了。桌面上的马屁，大家见怪不怪，当然是以厅长为核心了。开口闭口都是“x厅如何”“x厅如何”。 <br />
　　 <br />
其中一个局长，政绩相当强，也很得民心，在我们省也是一个名人了，多次受到表彰。厅长很是欣赏他，常和我父母说起，我父母又和我说起。 <br />
　　 <br />
我注意到，整场饭局，只有这个局长不怎么拍马屁。很自然的靠在椅背上，也不怎么说话，挺温柔的看着我们大家。当然了，该敬的酒他还是会敬的。 <br />
　　 <br />
饭局临近结束，厅长客套的说了一句：“再坐一会儿咱们就散了吧，今天也喝得差不多了，下次有时间我们再聚。” <br />
　　 <br />
其他人都连忙附和说好好，随时都可以。只有那位明星局长看着厅长，相当自然的说了一句：“不存在时间问题。您的时间，就是我们大家的时间。” <br />
　　 <br />
这马屁拍的！于平地中见惊雷。我妈妈当时就忍不住了，笑着指着那位局长说：“你这马屁拍的太到位了！这多少年的功力了？”全桌人都笑了起来。 <br />
　　 <br />
该局长一点不脸红，不慌不忙的喝了一小口酒说：“那是，为gc党打工了这么多年。早活明白了。”全桌人又是爆笑，厅长也忍不住笑了。 <br />
　　 <br />
他还是靠着椅背上，也不看厅长，接着说：“我这辈子生活也无忧了。今天有点小成绩，全靠组织提携。所以我这话说得也不假。不算是马屁。组织是我的衣食父母啊。对不对？” <br />
我妈妈说：“你说得也是。” <br />
他继续说：“所以说，组织上交待的工作，我肯定不马虎。工作之外，组织的时间，依然是我的时间。组织需要我，我就会出现。” <br />
　　 <br />
一个局长起哄的说：“xx这话说得就是到位。” <br />
我妈妈忍不住笑话他：“你就没他活得明白。” <br />
　　 <br />
饭局结束。该明星局长拍拍厅长的肩膀，说声走了哈。厅长连说好好。他转身就闪人了，头也没回。剩下几个局长依然凑一堆儿等厅长先离开，厅长没潘们，说声都回去吧，上车走了。 <br />
　　 <br />
或许我看到的并不是事实的真相。可能该厅长任人唯贤，很早就欣赏这位明星局长的超强业务能力；又或者他们之间有着什么见不得人的合伙勾当，导致两人关系极好。但是，在场面上他确实做得非常到位。马屁拍到位了，其个人的气场也保持得相当良好，完全不是阿谀作态。没有人厌恶或者轻视他。我父母回家以后，也对他赞不绝口。据说年后就要升官，现在应该已经履新了吧。 <br />
　　 <br />
这就是功力。 <br />
　　 <br />
案例四。 <br />
　　 <br />
这个案例我之前在群里讲过。也是这次回国了。常和几个有点小实权的兄弟去一个高档会所挥霍金钱。（我是蹭饭的。） <br />
　　 <br />
武汉是个经济不发达的城市。所谓的“高档”地方，和北京上海十年前差不多。吃燕鲍翅之类，喝些听起来很牛逼的酒，mm则是各种着装，OL，护士，比基尼，每一类都有三十多人，分别鱼贯而入，任君挑选。都是土了吧唧的官员被人请来玩的，没准中午还是在家喝的稀饭。谁要是自己花钱来消费，谁就是sb。 <br />
　　 <br />
不过在武汉，大多数还是没去过这样的地方的。带我来的这几个流氓，拿这地方泡妞，或者是打压妞的男朋友，相当有效果，一弄一个准。人家来了，多少有点拘束，桌上小火炖点鱼翅雪蛤什么的，也不知道该什么时候动筷子。相当能满足这群流氓的阴暗心理。 <br />
　　 <br />
那天还没点菜，带头大哥就琢磨叫谁过来买单。另一人提议说：“就xx吧，他挺会来事的，让他把弟弟也带过来。”据说这位小弟弟人见人爱，和谁都没个正经，特可爱。 <br />
　　 <br />
过了会，xx有事没能来，他弟弟一个人来买单了，很轻松的样子。坐定进餐，没一会鲍鱼来了。每人一个很精致的盅，里面就两小片糊弄人。这时，该位小弟弟用勺捞起鲍鱼，放在眼前仔细研究了一番，哧溜一下就吞了进去。咀嚼了好半天，很幸福的咽了下去。然后微笑着对我们说：“我靠，你们城里人是挺会玩的哈，我在农村从来没去过这么好玩的地方。” <br />
　　 <br />
所有人都乐了。大哥训斥他：“就你鬼话多。” <br />
　　 <br />
吃完了时间还早，没到叫mm的时候，开了一间房打麻将。小弟弟就坐在我旁边看，时不时指点我一下。我说，你也来搓来吧呗。他摇摇头说：“不会玩，你们这打得太高级了。我只会玩农村麻将。”众人皆笑。 <br />
　　 <br />
又过了一会。他说，“这地方我实在待不了，憋得慌，领导们继续fb吧。我买个单就回乡下了啊！” <br />
大哥说：“又回去搞老婆啊？多没意思，晚上就留这里玩啊。” <br />
小弟弟说：“不敢玩，老婆在jj上撒粉了。玩了要跪搓板的。” <br />
　　 <br />
我们狂乐。他自顾自走了。不一会儿服务生按人头送上来数条黄鹤楼1916. <br />
　　 <br />
一兄弟说：“这人办事很到位，心里有数，那些跟前跟后的很烦人。” <br />
大哥说：“所以他们拿的工程多啊。其他人请一年的饭都拿不下一个工程。就看那个样子，给我一百万我都不敢把工程给他做。做垮了我得进班房啊。” <br />
　　 <br />
四个和约会完全无关的案例写完，本节也就结束了。我写了接近两万字，内容已经超过了一般意义上的“人不求人一般高”。归根到底，是一个气场平衡，不在精神上被人控制的问题。 <br />
　　 <br />
赵敏的故事。说得是看上去再优质的美女，也是一个普通人，也必定体会过普通人的各种情绪。男人不可以自我矮化。 <br />
　　 <br />
悟空和八戒的故事。说的是一个看上再娇弱再需要照顾的小mm，她也是一个有完全责任能力的成年人。男人不可以自我高大，在关系不熟的情况下，不能立即充满保护欲。这容易让男人轻易的投资感情，到后面就越来越难以自拔。 <br />
　　 <br />
而这次的四个案例，和具体操作有关。 <br />
　　 <br />
前两个案例说的是，再牛逼的人，在毫无索取，母爱般温柔的眼神下，他也会变成一个小宝宝。我本人并非精通此道，已然获益匪浅。 <br />
　　 <br />
后两个案例说的是，哪怕是分明不平等的情况，哪怕你不得不按潜规则办事，哪怕你明明“有求于人”，你都可以做到气场的平衡。不付出是不可能的。我们要做的是：只付出自己该付的那一份，把自己该拿的东西拿到手，不做别人的奴隶。 <br />
　　 <br />
（本节完）</div>

							
							
							
							
