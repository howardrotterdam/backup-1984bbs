																											<div id="postmessage_499464" class="t_msgfont">回顾一下我杀死七只老鼠的七种方法。<br />
1，挤杀。<br />
这是第一次对付入宅的老鼠。是半夜。起初，它窜到窗帘顶部，我去打，结果Y跳下，钻到床头柜后面。我扒开去看，发现它已爬到半中腰，见我后立刻下窜，我立马一推，只听“吱”的一声。挪开床头柜一看，墙上一抹血，老鼠已死，挤死的。曰挤杀。<br />
2，锤杀。<br />
发现客厅空调下方那个洞是其藏身之处。于是，我买了两帖老鼠粘。一帖放在洞外，一贴洞内。夜晚回家后，发现外面一贴上有鼠毛，还有胶抹在地上，心想：挣脱后跑了？打开盖子一看，嘿嘿，Y被牢牢粘在洞内那帖上。哈哈，好啊，看我如何收拾你：合上老鼠粘，拎到露台，对准头部，一铁锤下去。曰锤杀。<br />
3，淹杀。<br />
这次我有损失，Y把我的窗帘咬出一连串洞。原因是我发现它是半夜，怕影响楼下，暂且关在电脑房一晚，第二天白天才去收拾。打得它无处可逃，它只好窜到桌子上的干花瓶里，它以为安全了。我迅速找来一个铁质饼干盒，先抽去干花，再把盒子盖上，然后拿到厨房，移开盒子露出一条缝―――灌水。灌满后，只听盒盖碰碰作响――Y憋得慌，用头想撞开盒子。NND，休想！死摁了半天，没动静了。掀开一看，Y已头朝下了。曰淹杀。<br />
4，喷杀。<br />
这次好恶心。它趴在窗帘上方（我不知道），睡前光着身子去关窗，谁知Y来了个“二级跳”――先跳我肩膀上，再跳下地，然后就想溜出卧室。不料，卧室门是关着的（夏天开空调，老婆恰好出差），Y只好回头钻进卫生间。我把门一关，想着如何收拾它。想来想去，拿来一罐喷蟑螂的喷雾剂，对着卫生间下方缝隙往里喷。喷啊喷，整罐喷完了，Y满身“水”，但就是没事，我也没招了。等吧，等明天再收拾。可是，睡到半夜，听到卫生间门下方的缝隙上Y在抓来抓去，尾巴正好露在外面。我一看，有办法了，赶紧找来一把老虎钳，一下就钳住了Y的尾巴。高兴之余我又傻了――老虎钳不能松开，它在里面乱蹦Q，怎么办？叫我妈来帮忙。可千呼万唤她也听不见，我只好捡起空罐猛地摔向卧室门。这一摔老妈听见了。“儿子你在干什么？”老妈披着衣服进来了。我说你再找一把老虎钳，从里面钳它。可我左手怎么也钳不住它甩来甩去的头。怎么办？继续喷。又拿来一瓶喷雾剂，直接对着鼠头喷。不一会，鼠头耷拉下来。此时我才松开老虎钳，使劲夹着鼠头扔了出去。此曰喷杀。<br />
5，戳杀。<br />
这一次它钻到衣柜与墙壁之间的缝隙里，缝隙很窄，Y无法转身，我便找来一根扁棍，径直戳去，戳了多次，戳一次，Y吱一声，戳一次吱一声，直至不再动弹。曰戳杀。<br />
6，压杀。<br />
Y被打得乱窜，最后进了厨房，爬上窗户栏杆。我把门一关，准备找东西收拾。我找来几根竹子，攥在一起，准备开门去打。透过玻璃我发现，Y在窗户上伺机跳下，企图在我开门一瞬间逃走。无奈我开门关门速度极快，当Y跳下跑到门前时门已关严。Y只好继续乱跑，我拿竹棍追打，直把竹子全部打碎（竹子在露台放了多年，已竹质疏松），但多少也打中了几下――Y跑时已显跛相。最后Y钻进垃圾桶下面那个小洞里。但洞内空间有限，Y已无法转身，且尾巴露在外面。怎么办？我灵机一动，一边用脚踩住尾巴，一边将垃圾桶轻轻抬起一点，让其半截身子露在外面，然后把垃圾桶的下沿使劲压下――呜呼，此次Y实际上被腰斩了。此曰压杀。<br />
7，晒杀。<br />
就是上面那次。是先用老鼠粘粘住，然后装入一个盒子扔进楼梯的垃圾桶。准备上班时又觉得太便宜它，于是又捡起盒子，拿到天台去晒。那天正好太阳贼毒，我将盒子盖掀开，立在墙角，将Y的身子完全暴露在阳光下。起初，Y还鼠目四转，半小时后，Y已晒死。这次可过了瘾解了恨。曰晒杀。<br />
残忍吗？呵呵，管它呢，四害之一嘛，怎么收拾都不过分。<br />
而且，低劣物种怎么都消灭不完，因为它们自有生存技巧：用数量的巨万抵消质量的低劣。</div>

							
							
							
							
