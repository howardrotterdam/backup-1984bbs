你现在有两个选择，教师或者自己找别的工作。这两个选择中只有能选教师，因为你是在中国，低级别的体制外工作是很不稳定而且辛苦的。不是每个体制外的人都在Google，微软的。大部分人只能在一个很小公司。而高级别的体制外人士几乎很少是由低级别的体制外人士干出来的，例如媒体界的很多自由媒体人实际上人家一毕业都是在政府办的报纸上班。偶尔有几个成功的例子，这些人也是积极的向体制靠拢，例如你很难解释一个大型的私营证券公司算是体制内还是体制外。总的来说，在国内体制外几乎没戏，低级别的体制外更加没戏。<br />
<br />
但是做教师尽管在体制内，很稳定，可惜发展的范围很小，你去当教师的第一天，看看同一个办公室的40多岁的教师，你就可以知道你40多岁的样子了。<br />
<br />
