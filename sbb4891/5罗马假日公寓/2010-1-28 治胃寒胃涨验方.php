																											<div id="postmessage_558722" class="t_msgfont">[编辑本段]碳14呼气试验<br />
<br />
　　尿素[14C]呼气检测是目前检查幽门螺旋杆菌（简称Hp）的方法。对幽门螺旋杆菌的准确检测能有效地预防胃溃疡、慢性胃炎、胃癌的发生。由于不需要取得胃内的活检组织，也不用抽血，病人只需轻轻吹口气，通过化学试剂的变化，即可在30分钟内准确诊断是否有幽门螺旋杆菌感染。<br />
　　原理<br />
　　哺乳动物细胞中不存在尿素酶，而Hp富含高活性尿素酶。虽然尿素酶并不只是Hp所特有，但胃内存在尿素酶是Hp存在的证据，因为胃中尚未发现有其它种类的细菌。为了检测胃内Hp，予受检者口服尿素[14C]胶囊(0.75uCi), 胃内Hp产生的尿素酶催化尿素迅速水解成NH4+和H 14CO3- ，后者吸收入血液经肺以14CO2 形式呼出，收集呼气标本并测量14CO2 ，便可判断有无Hp的感染。<br />
　　13/14C-尿素呼气试验基本原理<br />
　　简便、快速、准确、无痛苦、无创伤、无交叉感染的风险，是尿素呼气试验法诊断的最大优点。大多数地区均已入地方医保，价格比胃镜便宜很多，一般医院90-120元/次不等。适用于临床、体检。<br />
　　呼气试验全过程需时约30分钟。简单说来，患者在清晨空腹或进食两小时后，用约20ml凉开水口服一粒尿素[14C]胶囊，静坐25分钟后，用一次性吹气管向二氧化碳吸收剂中吹气，再将吹完气的样品交给医生做检测，检测就完成了。（吹气时间约需1～3分钟，若超过5分钟褪色不全，亦停止吹气，此时CO2吸收已饱和，但因唾液等进入干扰了非水滴定系统而影响变色，并不影响测试结果。）<br />
　　呼气试验法可适应下列疾病的检查：<br />
　　1、消化不良初诊者（病史不长，年龄小于45岁并无胃癌 报警症状）、复诊者；<br />
　　2、胃十二指肠溃疡、慢性活动溃疡、胃窦炎、胃黏膜相关性淋巴样组织恶性淋巴瘤等需根除 Hp的患者；<br />
　　3、预防胃癌或有胃癌家族史者；<br />
　　4、拒绝胃镜检查者（除非疑诊胃癌时才进行胃镜活检）；<br />
　　5、长期使用NSAID（非甾体抗炎药）类药物者等等。<br />
　　注意事项<br />
　　&#61548;孕妇、哺乳期妇女不宜做此试验。<br />
　　&#61548;一个月内使用抗生素、铋制剂、质子泵抑制剂者，五天内急性胃出血者不宜做。<br />
　　&#61548;不能咬破胶囊。胶囊如有破损不能使用。<br />
　　&#61548;吸收剂和闪烁液严禁内服。<br />
　　&#61548;吸收剂如有少量吸入口中，请立即吐出，用清水漱口。<br />
　　&#61548;闪烁液如洒到眼睛等敏感部位，立即用大量清水冲洗。<br />
　　尿素[14C]呼气试验临床试验总结<br />
　　通过北京协和医院、北京医院、北京同仁医院等六家权威医院的临床试验，得出结果为14C-UBT对Hp感染诊断的敏感性为96.5%，特异性为91.8%，准确率为94.6%，结论：海得威生产的14C-UBT药盒(0.75uCi)符合临床Hp诊断要求。<br />
　　14C-UBT有5S特征：Sensitive, specific, speed, simple, safe. (敏感、特异、快速、简单、安全)<br />
　　尿素[14C]呼气试验检查的适应症<br />
　　普通人群的Hp感染率十分高，尤其在发展中国家，感染率可高达40～60%，感染了Hp是否会发病，导致何种疾病则由Hp的基因特征、宿主基因特征和环境影响三方面共同作用决定。目前临床上可列为常规检查的情形是消化不良、消化性溃疡、慢性胃炎、胃癌及胃癌家族史阳性成员、长期使用NSAID类药物的病人。<br />
　　（一）消化不良初诊者：<br />
　　&nbsp; &nbsp;&nbsp; &nbsp; 根据病史和体检提示上消化道疾病所致者，以往未作过胃镜检查，若病史不长，年龄小于45岁（我国属胃癌高发区，建议界限定于30岁左右），无报警征兆（明显恶心、呕吐、腹痛、厌食；出血、贫血、黄疸、腹块、体重下降），可先作UBT检查Hp；阳性者予根除Hp治疗，无效时再考虑胃镜或其它检查。这就是目前国际上流行的“检查－治疗方案”。对消化不良初诊者一律先作胃镜检查再治疗，或一概先作抗酸、动力药经验性试验治疗，亦或全部先作抗Hp治疗的主张和行为都是不太适合的。<br />
　　（二）消化不良复诊者：<br />
　　若已作过胃镜，并明确了诊断，除非考虑胃癌，作UBT检查即可，不必重复胃镜。<br />
　　（三）胃镜检查者<br />
　　1，胃镜消毒效果无可靠保障时，为降低致病病毒传播的风险，可用14C-UBT替代尿素酶试验，活检只用在疑诊胃癌时。<br />
　　2，由于快速尿素酶假阴性高，所以对胃镜活检组织进行快速尿素酶试验阴性者，仍怀疑Hp感染，可加做一次14C-UBT，以明确诊断，降低快速尿素酶试验的误诊率。<br />
　　（四）拒绝胃镜检查者<br />
　　（五）不能耐受胃镜检查者（如有心脏病、病毒性肝炎、急性咽喉炎、高血压病人等）<br />
　　（六）Hp根除效果的跟踪：<br />
　　&nbsp; &nbsp;&nbsp; &nbsp; 由于抗生素在Hp感染治疗中的应用，Hp耐药株的发生率不断上升，以致Hp根除的难度越来越大。因此可将尿素[14C]呼气试验用于病人做根除治疗后效果的评价手段，给临床治疗方案的调整提供依据。<br />
　　尿素[14C]呼气试验的安全性<br />
　　（一）尿素[14C]呼气试验对患者和操作人员都是安全的<br />
　　尿素[14C]呼气试验已经投入临床应用十几年，未见到任何不良反应的报道。迄今为止，所有关于尿素[14C]呼气试验安全性的专业评估报告结果都是：对患者和操作人员的辐射危险可忽略不计。<br />
　　这是基于如下理由：<br />
　　1．14C的辐射能量极弱，其释放的低能纯β射线穿透力极弱，0.3mm的水或一张纸（胶囊）即可将其阻挡。<br />
　　2．14C-UBT使用的尿素[14C]物理剂量几可不计，放射性剂量0.75-1.00&micro;Ci，做一次检查照射剂量不及一天自然本底照射剂量或相当于坐1小时飞机旅行受到的辐射，其辐射几乎可以忽略不计，可见其危害之小。<br />
　　天然辐射是一种普遍现象，14C在自然界广泛存在，在我们周围的空气、土壤、水体、动植物、甚至人的本身中都含有天然的14C，陆生生物界包括人在内的每克碳中的14C为13.6dpm，成人14C总量为20，000dpm～30,000dpm。而尿素[14C]呼气试验诊断中强阳性呼气样本一般仅仅含有约3000dpm的14C,可见，其安全性是不容置疑的。<br />
　　3．尿素[14C]的生物半衰期极短，虽然14C的物理半衰期长达5730年，但尿素[14C]的生物半衰期很短，尿素形态和CO2形态是人体代谢的终极产物，未被分解的尿素以原形从尿中排出，摄入的14C不会转化为人肌体的一部分，48小时可基本排出体外，所以不会对人造成长期影响。<br />
　　（二）尿素[14C]呼气试验对环境没有影响<br />
　　迄今为止，所有关于尿素[14C]呼气试验对环境影响的专业评估做结论都是：尿素[14C]呼气试验对环境没有影响。<br />
　　这是基于如下理由：<br />
　　1．14C是一种天然同位素，由宇宙射线与大气的作用而不断产生，地球上的一切含碳无机物、有机体、土壤、河流、海洋中都含有天然的14C，现代的核试验、核电站的运行也在不断产生大量的14C， 尿素[14C]呼气试验导致增加的量微不足道。<br />
　　2．14C进入环境后，立即参与地球上的大气圈―生物圈―水圈的循环并达到平衡，不存在聚集现象。<br />
　　3．14C-UBT对环境的影响，国家环保局为此专门作了评估。报告指出，如果全所接受14C-UBT的人数达510万，每人接受剂量为1&micro;Ci，则全年14C总产量为2*1011Bq，假定全部释放到大气中，也仅占1989年全世界核反应堆释放到大气中14C量的2/1000。1993年报告，天然14C生长量约为1×1015Bp，14C-UBT给大自然环境中提供的量是微不足道的。<br />
　　（三）尿素[14C]呼气试验在国内外均已被豁免管理<br />
　　&nbsp; &nbsp;&nbsp; &nbsp;正是由于科学的事实和充分的理由，国内外均已对尿素[14C]呼气试验给予豁免管理。<br />
　　1．美国核条例委员会于1997年12月2日批准14C-UBT药盒的使用可豁免放射药品管理，明确指出：任何一个内科医生可以在其办公室内为病人进行尿素[14C]呼气试验检查。<br />
　　2．中国国家环保总局2002年批准14C-UBT药盒用于Hp感染体内诊断可按放射性药品豁免管理。也又由于尿素[14C]容易制备，14CO2测试仅需不贵的液体闪烁计数仪，费用适中，故该试验受到病人和医生的普遍欢迎。<br />
　　（四）尿素[14C]呼气试验开展八年来，未见有任何不良反应报道。</div>

							
							
							
							
