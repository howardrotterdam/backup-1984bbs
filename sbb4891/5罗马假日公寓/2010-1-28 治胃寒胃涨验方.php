													<h2>幽门螺旋杆菌是什么时候发现的</h2>
						
						
																											<div id="postmessage_464169" class="t_msgfont">80年代初，对胃炎、胃癌及上消化道溃疡的诊断和治疗的理论依据，被一种只有在显微镜下才看得到的幽门螺旋杆菌所冲击。既往传统的“无酸无溃疡”的观念被质疑，原来用抗酸、抑酸和保护胃粘膜来治疗胃炎和胃溃疡，现在用抗菌素亦能有效。回顾这一微生物的发现简史，或许对今后医学研究有所帮助。<br />
一、幽门螺旋杆菌(Helicobecter Pylori)发现前期<br />
&nbsp; &nbsp; 在上消化道溃疡者的胃粘膜中发现有细菌，可以一直追溯至1874年。翻开历史，在19世纪70代，正是医学界开始重视和推广奥地利医师Semmelweis的发现的时候。他在1861年完成凝结他心血的著作《产褥热的病因、认识及预防》中，指出细菌是产褥热的病因。由于他的发现，产褥热引起的产妇死亡率由18%下降至2-1%，在当时无疑是一个轰动。可是，显然是由于检测手段的落后，当时的发现未被承认，自那以后很长时间，只是时有不甚完整的零星报道。<br />
&nbsp; &nbsp; 1865年Semmelweis去世时，正是Pasteur开始为法国养蚕业效力之日。Pasteur对细菌的研究，奠定了现代微生物学，至今仍是现代医学的基础。<br />
&nbsp; &nbsp; 1893年意大利病理学家Bizzozero首次报告在哺乳动物胃内发现螺旋形微生物(spiral organisms),他在研究各种动物胃肠道上皮的微观形态过程中，在6只狗的胃腺和壁细胞内观察到螺旋体(spirochaetes)。1896年，Salomon证实了狗胃内螺旋形微生物的存在，并在猫和大鼠的胃内也发现了类似的生物。1906, Krienitz首次证实在人胃癌患者中的溃疡表面坏死物和胃内容物中存在螺旋体(human gastric spirochaetes)。9年之后，Rosenow在胃和十二指肠溃疡患者胃中发现螺旋形微生物，但认为这些微生物来自口腔的污染。<br />
1938年，Doenges报道了在恒河猴和人类尸解的胃腺体内发现一种螺旋体样的生物体。<br />
&nbsp; &nbsp; 1940年，Freedberg和Barron通过长期研究，用银染色在35例胃切除术的标本中，发现有13例存在螺旋体。在无溃疡的地方，几乎没有微生物，他们得出的结论是:植入良恶性溃疡周围组织中的细菌是非致病性的(non-pathogenic oppontunists)。<br />
&nbsp; &nbsp; 1954年，Palmer检查了1140例胃活检标本，发现“没有任何理由证明是螺旋体的特质引起结构改变”。他认为在靠近溃疡和幽门的螺旋体是多源的，可能是口腔污染。此后，除了好奇所至，偶而提提，螺旋体几乎很少有人再提及。十分遗憾的是，Pelmer没有用银染色。这一遗漏，使幽门螺旋杆菌的发现，后移了近30年。<br />
&nbsp; &nbsp; 60年代后期的一些重要研究，如1967年Gray和Shiner关于《胃及空肠菌群对胃pH值的影响》的研究和1969年Drasar等人《健康人及酗酒人胃肠道菌群的研究》均未发现幽门螺旋杆菌。其原因可能是该菌培养时对环境有特殊要求及其生长缓慢所致。<br />
在这一时期，可以说人类对这一细菌所知略微。<br />
二、幽门螺旋杆菌的发现<br />
&nbsp; &nbsp; 1975年，Steer描述了胃炎患者的胃粘膜层存在一种与胃粘液紧密相关的细菌，在正常人的胃中无法找到。Steer发现这种细菌通常“粘附着胃粘液分泌细胞，一头至少有一个丝状突起”。Steer进一步在胃溃疡患者中对它进行了超微结构研究，发现这种细菌呈螺旋形。他虽未详细描述它的形态学特征，但却令人信服地证明这些细菌不是活检时污染的，并强调中性白细胞通过胃粘膜的迁移，可能是对这种细菌的反应。<br />
&nbsp; &nbsp; 遗憾的是，当时对这些内窥镜活检标本均未能培养出结果。Steer宣称它是一种“假性铜绿染色(Pseudomonas aeruginosa)&quot;，而非螺旋形微生物。几乎完全肯定它是种间接的污染。<br />
&nbsp; &nbsp; 1979年，Fung等人在研究慢性胃炎的上皮组织和超微结构时，亦发现了它的踪迹，观察到上皮细胞之间的沟内及胃小凹内有细菌存在，可惜未做深入研究。<br />
&nbsp; &nbsp; 1983年，Steer显然一直未忘怀几年前的研究，用电子显微镜扫描发现这种螺旋菌大量存在于胃幽门前区的变形组织和十二指肠根部的胃型上皮细胞表面。在肠型上皮细胞表面找不到它;在十二指肠溃疡病人中有73%可以看到。<br />
&nbsp; &nbsp; 可以说，Steer比前面任何人都更接近于发现了幽门螺旋杆菌，但他却没有确定它。<br />
&nbsp; &nbsp; 在Steer用电镜来进一步探讨他1975年的发现前，世界各地已有不少科学家在加紧这方面的研究。<br />
&nbsp; &nbsp; 1980年，西澳大利亚皇家朴茨医院病理科医师Warren在患有胃炎和上消化道溃疡患者内窥镜活检标本上观察到曲形和S形的细菌，紧密地粘附在表面上皮细胞上，在胃小凹之内或之间可存在。细胞可以被Warthin-Starry方法(银染色)所染色，在常规的苏木精和伊红染色下难以见到。到1982年，Warren已发现了135例，与他的同事Marshall将它们培养存活。研究结果不是以论著形式发表，而是以书信形式发表在英国著名杂志，《柳叶刀》中给编辑的书信栏目中。第一封是Warren的信，谈及这一细菌的发现。另一封是Marshall的信，对该细菌进行了形态学的描述。这种研究的发表形式可能有两种解释，一种是撰写者急欲发表，还有一种是反映了作者与编者在公布这一发现时的谨慎态度和生怕出错的心态。很显然，当时作者和编者都意识到，这一发现一旦被肯定，将对医学界有一重大冲击。 Marshall在信中最后写道:“如果这些细菌真的象Warren描述的那样，伴生于幽门胃炎，那么，它们可能对我们目前只有很少了解的胃炎相关疾病(即上消化道溃疡和胃癌)承担部分角色”。<br />
&nbsp; &nbsp; Warren写道:无炎症的地方，则很少发现细菌，经常在慢性胃炎时发现它们。曲菌几乎总是多发于活动性慢性胃炎，常常是大量的，经常生长在表面上皮细胞之间，曲菌和伴随而来的组织等改变可能表现在胃的任何部分，但绝大多数发生在胃幽门部。无细菌的炎症，发生在局部损害附近，诸如癌或上消化道溃疡的粘膜。在这些例子中，白细胞通过厚厚的粘膜层迁移，伴随着细菌的表面浸润形成鲜明对比。细胞和典型的组织改变常常在未被局部损害影响的粘膜层发现。<br />
&nbsp; &nbsp; Marshall的信一开头就提出一个问题:为什么它们以前未被发现，它们是病原菌还是在被损害的粘膜中罕见的共生现象，它们是弯曲菌(Campylobacters)吗?<br />
&nbsp; &nbsp; Marshall以幽门活检标本用弯曲菌隔离技术进行培养，最初的34例活检标本在标准的弯曲菌和非选择性介质中按常规培养48小时，均告失败。第35例标本是在1982年复活节那天开始接种的，放入孵箱后，便开始了度假。5天后检查时，发现在非选择性的培养基中，有一层厚厚的细菌样的弯曲菌生长。多亏了这个假期，否则，不知何时才能发现它。生长期较长，是这一细菌的一大特性，也是许多人不获成功的原因之一。<br />
&nbsp; &nbsp; 在37℃的巧克力培养基上，3-4天可以长成一层半透明层，短螺旋体直径0.5um，长度约2.5um。该细菌有平滑的外膜，一端可以长出大约5个左右的带鞘鞭毛。在某些情况下，鞭毛可发生在两端，这些细菌在胃以外的任何器官都分离不到，在自然界中也无法培养出来，而且不能归类于当时已知的任何一种形态和生化学的菌种。<br />
1984年，Marshall将其命名为幽门弯曲菌(Campylobacter pyloridis)。1985年这一命名在《国际系统微生物学杂志》上，被学术界认可。1987年，因语法有误，这一命名被改为Campylobacter pylori。<br />
&nbsp; &nbsp; 1984年，Rollasen等人通过内窥镜活检，对人胃中的这一微生物进行研究并指出，这一细菌是与组织性胃炎明确相关的，但与胃癌、上消化道溃疡和胃的抽吸物的高pH值无关。<br />
&nbsp; &nbsp; 同年，Jones等人对它们进行组织学和血清学研究。他们发现:在革兰氏片上(gramfims)，细菌的生长呈典型的弯曲形状。90%的异常组织均可培养出它来。更重要的是Jones发现它们有血清学上的意义。他们用兔血清免疫，同时用对已被公认的人体共生菌C jejuni抗血清做交叉试验。结果发现，在34例阳性血清中，只有6例17%对C jejuni起反应。与Warren和Marshall不同的是，他们发现该细菌不仅发生在活动性胃炎，而且在非活动性胃炎上，也能发现它，两者无明显区别。<br />
&nbsp; &nbsp; 实际上，在1980年以前，世界上至少有3个不同的实验室看到了胃的螺旋菌，他们是:Steer所在的英国南安普敦实验室，Rollasen所在的美国伯明翰实验室及Warren和Marshall所在的西澳大利实验室，但只有后者最先培养成功。这也可以帮助我们理解为什么他们要以书信形式抢先在《柳叶刀》上发表他们的研究。<br />
&nbsp; &nbsp; 到1986年，英格兰、荷兰、德国、加拿大、日本、秘鲁及中国等地的实验室纷纷从胃炎和胃溃疡患者身上分离培养成功幽门弯曲菌，一时间，世界各地的临床病理学家、胃肠病学家将注意力转向这一小小的微生物。<br />
一些新的检测技术如快速尿素酶试验，14c、 13C呼吸试验，Giemsa染色法以及PCR技术被广泛用于Hp的感染诊断及流行病学调查。1988年Leunk发现不同来源的Hp菌株有58%可分泌直接使细胞空泡变的物质VacA，并发现与其同时表达的相关蛋白CagA，认为两者是Hp的主要致病因素。随后，人们还发现Hp感染时宿主出现胃泌素/生长抑素分泌紊乱、炎性介质释放、氧自由基形成、免疫损伤、胃上皮增殖与凋亡失衡、癌基因与抑癌基因改变等诸多变化。<br />
&nbsp; &nbsp; 1989年，Goodwin等人从超微结构及形态学、细胞脂肪酸、基因序列、生长特征和酶功能等5个方面研究指出，幽门弯曲菌原来分类命名的根据不足，它的基因序列既不属于弯曲菌，也不属于弯曲菌所属的Wolinella菌属。所以，他们建立了一个新属，将这一新发现的细菌命名为一个新的名字:幽门螺旋杆菌(Helicobecter pylori)。这一命名很快就被学术界承认。Helicobecgter一词反映了这种细菌的两个形态学特征，在体内呈螺旋形，在体外呈杆状。<br />
&nbsp; &nbsp; 自此，对幽门螺旋杆菌的发现可以告一段落，1997年美国的基因研究所成功地完成Hp全基因序列的测定，使人们对它的认识更趋完整。</div>

							
							
							
							
