																											<div id="postmessage_478367" class="t_msgfont">中华人民共和国户口登记条例　　（1958年1月9日全国人民代表大会常务委员会第九十一次会议通过，1958年1月9日中华人民共和国主席令公布，1958年1月9日起施行。）<br />
　　第一条 为了维持社会秩序，保护公民的权利和利益，服务于社会主义建设，制定本条例。<br />
　　第二条 中华人民共和国公民，都应当依照本条例的规定履行户口登记。<br />
　　现役军人的户口登记，由军事机关按照管理现役军人的有关规定办理。<br />
　　居留在中华人民共和国境内的外国人和无国籍的人的户口登记，除法令另有规定外，适用本条例。<br />
　　第三条 户口登记工作，由各级公安机关主管。<br />
　　城市和设有公安派出所的镇，以公安派出所管辖区为户口管辖区；乡和不设公安派出所的镇，以乡、镇管辖区为户口管辖区。乡、镇人民委员会和公安派出所为户口登记机关。<br />
　　居住在机关、团体、学校、企业、事业等单位内部和公共宿舍的户口，由各单位指定专人，协助户口登记机关办理户口登记；分散居住的户口，由户口登记机关直接办理户口登记。<br />
　　居住在军事机关和军人宿舍的非现役军人的户口，由各单位指定专人，协助户口登记机关办理户口登记。<br />
　　农业、渔业、盐业、林业、牧畜业、手工业等生产合作社的户口，由合作社指定专人，协助户口登记机关办理户口登记。合作社以外的户口，由户口登记机关直接办理户口登记。<br />
　　第四条 户口登记机关应当设立户口登记簿。<br />
　　城市、水上和设有公安派出所的镇，应当每户发给一本户口簿。<br />
　　<font color="#DC143C">农村以合作社为单位发给户口簿；合作社以外的户口不发给户口簿。</font><br />
　　户口登记簿和户口簿登记的事项，具有证明公民身份的效力。<br />
　　第五条 户口登记以户为单位。同主管人共同居住一处的立为一户，以主管人为户主。单身居住的自立一户，以本人为户主。居住在机关、团体、学校、企业、事业等单位内部和公共宿舍的户口共立一户或者分别立户。户主负责按照本条例的规定申报户口登记。<br />
　　第六条 公民应当在经常居住的地方登记为常住人口，一个公民只能在一个地方登记为常住人口。<br />
　　第七条 婴儿出生后一个月以内，由户主、亲属、抚养人或者邻居向婴儿常住地户口登记机关申报出生登记。<br />
　　弃婴，由收养人或者育婴机关向户口登记机关申报出生登记。<br />
　　第八条 公民死亡，城市在葬前，农村在一个月以内，由户主、亲属、抚养人或者邻居向户口登记机关申报死亡登记，注销户口。公民如果在暂住地死亡，由暂住地户口登记机关通知常住地户口登记机关注销户口。<br />
　　公民因意外事故致死或者死因不明，户主、发现人应当立即报告当地公安派出所或者乡、镇人民委员会。<br />
　　第九条 婴儿出生后，在申报出生登记前死亡的，应当同时申报出生、死亡两项登记。<br />
　　第十条 公民迁出本户口管辖区，由本人或者户主在迁出前向户口登记机关申报迁出登记，领取迁移证件，注销户口。<br />
　　公民由农村迁往城市，必须持有城市劳动部门的录用证明，学校的录取证明，或者城市户口登记机关的准予迁入的证明，向常住地户口登记机关申请办理迁出手续。<br />
　　公民迁往边防地区，必须经过常住地县、市、市辖区公安机关批准。<br />
　　第十一条 被征集服现役的公民，在入伍前，由本人或者户主持应征公民入伍通知书向常住地户口登记机关申报迁出登记，注销户口，不发迁移证件。<br />
　　第十二条 被逮捕的人犯，由逮捕机关在通知人犯家属的同时，通知人犯常住地户口登记机关注销户口。<br />
　　第十三条 公民迁移，从到达迁入地的时候起，城市在三日以内，农村在十日以内，由本人或者户主持迁移证件向户口登记机关申报迁入登记，缴销迁移证件。<br />
　　没有迁移证件的公民，凭下列证件到迁入地的户口登记机关申报迁入登记：<br />
　　一、复员、转业和退伍的军人，凭县、市兵役机关或者团以上军事机关发给的证件；<br />
　　二、从国外回来的华侨和留学生，凭中华人民共和国护照或者入境证件；<br />
　　三、被人民法院、人民检察院或者公安机关释放的人，凭释放机关发给的证件。<br />
　　第十四条 被假释、缓刑的犯人，被管制分子和其他依法被剥夺政治权利的人，在迁移的时候，必须经过户口登记机关转报县、市、市辖区人民法院或者公安机关批准，才可以办理迁出登记；到达迁入地后，应当立即向户口登记机关申报迁入登记。<br />
　　第十五条 公民在常住地市、县范围以外的城市暂住三日以上的，由暂住地的户主或者本人在三日以内向户口登记机关申报暂住登记，离开前申报注销；暂住在旅店的，由旅店设置旅客登记簿随时登记。<br />
　　公民在常住地市、县范围以内暂住，或者在常住地市、县范围以外的农村暂住，除暂住在旅店的由旅店设置旅客登记簿随时登记以外，不办理暂住登记。<br />
　　第十六条 公民因私事离开常住地外出、暂住的时间超过三个月的，应当向户口登记机关申请延长时间或者办理迁移手续；既无理由延长时间又无迁移条件的，应当返回常住地。<br />
　　第十七条 户口登记的内容需要变更或者更正的时候，由户主或者本人向户口登记机关申报；户口登记机关审查属实后予以变更或者更正。<br />
　　户口登记机关认为必要的时候，可以向申请人索取有关变更或者更正的证明。<br />
　　第十八条 公民变更姓名，依照下列规定办理：<br />
　　一、未满十八周岁的人需要变更姓名的时候，由本人或者父母、收养人向户口登记机关申请变更登记；<br />
　　二、十八周岁以上的人需要变更姓名的时候，由本人向户口登记机关申请变更登记。<br />
　　　第十九条 公民因结婚、离婚、收养、认领、分户、并户、失踪、寻回或者其他事由引起户口变动的时候，由户主或者本人向户口登记机关申报变更登记。<br />
　　第二十条 有下列情形之一的，根据情节轻重，依法给予治安管理处罚或者追究刑事责任：<br />
　　一、不按照本条例的规定申报户口的；<br />
　　二、假报户口的；<br />
　　三、伪造、涂改、转让、出借、出卖户口证件的；<br />
　　四、冒名顶替他人户口的；<br />
　　五、旅店管理人不按照规定办理旅客登记的。<br />
　　第二十一条 户口登记机关在户口登记工作中，如果发现有反革命分子和其他犯罪分子，应当提请司法机关依法追究刑事责任。<br />
　　第二十二条 户口簿、册、表格、证件，由中华人民共和国公安部统一制定式样，由省、自治区、直辖市公安机关统筹印制。<br />
　　公民领取户口簿和迁移证应当缴纳工本费。<br />
　　第二十三条 民族自治地方的自治机关可以根据本条例的精神，结合当地具体情况，制定单行办法。<br />
　　第二十四条 本条例自公布之日起施行。</div>

							
							
							
							
