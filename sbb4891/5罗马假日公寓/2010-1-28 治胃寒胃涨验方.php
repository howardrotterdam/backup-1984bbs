													<h2>一个文革狱卒的奇文</h2>
						
						
																											<div id="postmessage_476333" class="t_msgfont">期待遇罗克案张志新案真相<br />
<a href="http://www.vankeweekly.com/Forum/answertopic.aspx?ForumID=58&amp;TopicID=193243" target="_blank">http://www.vankeweekly.com/Forum ... &amp;TopicID=193243</a><br />
时间：2009/03/11 出处：搜狐博客 <br />
张志新之死何时公布真相　<br />
<br />
　　一、关于张志新死时描述的两点疑惑？ <br />
<br />
　　我在文革期间当了五年兵，看押了五年的犯人，以本人的亲身经历对张志新死后那批文人宣传的所谓事实提出疑问：第一：张在监狱中曾被轮奸。这是绝对不可能的，在那个年代，不论是什么犯人，一般都是集体关押，除了极个别的重刑犯是单独关押的，但纪律十分严格，不论是任何人的提审，都要经过严格的交接手续。做为警卫部队的哨兵一般只会一个哨位只有一个哨兵，而带班员（一般都是正副班长）或游动哨几乎十分钟就会来一次，还不时的有管教干部（警察）随时都可能出现，并且那时的军人是十分注重自己的政治生命的，如果当上几年兵入不了党，在退伍时都会在自己的老乡面前羞愧得抬不起头来，因此绝大多数军人是不可能去集体干这种事。至于说是警察干的也近于荒唐，首先在军人的眼皮底下不可能做得到，再说那个年代的警察基本上与军队性质差不多，而且军警本身就属于两个完全独立的系统，相互监督，即使有个别思想肮脏的人，在那个人人想立功授奖的年代中，想在这两个部门中找到与自己的同伙，无疑近乎天方夜潭！在我当兵五年的经历中仅听说过（还不是亲眼所见）以前有某个警察，被一女犯主动拉下水，犯了错误（而且根本就没有性交）。第二，张在行刑前被割断喉管。有头脑的人应当想一下，这也是不可能也完全没有必要的，这样做的目的无非是不想让她喊出口号来（且不去深究是革命的或是反革命的），这确实是当时对待死刑犯（特别是政治死刑犯）的一种措施，我在当兵五年中曾执行过多次警戒任务，且都是非常近距离的看到那些死刑犯们如何上路的。为了防止上述情况的出现，只要用吊背绊就可以达到这个目的，如果被执行死刑的犯人有这样的举动，只需要将其绊在背后的手臂向下稍压一下，犯人就会连呼吸都困难，更不要说喊口号了。试问有必要割断她的喉管来防止她呼喊口号吗？如果真这样做了，那么死刑犯执行枪诀之后的照片如何上报？　<br />
<br />
　　二、真实的“遇罗克”和“张志新” <br />
<br />
　　经常看到有人拿遇罗克和张志新说事，试图利用个案丑化文革。为了达到这个目的，“遇罗克”、“张志新”已经被涂抹的面目全非，完全失去了本来面貌。而本文试图还历史以真相，告诉你一个真实的遇罗克和张志新。 <br />
<br />
　　遇罗克谈不上什么“被害”，遇罗克在文革中被判死刑，原因是私藏手榴弹，预谋暗杀毛主席。1967年夏天，《中学文革报》的大部分成员到东北去看武斗的热闹，遇罗克在长春车站赶上发武器，就带了手榴弹回到北京，后来又害怕被查出来，想到香山埋了，就这样被***局盯上了。最后死刑判决书上写的也是：阴谋杀害伟大领袖毛主席。[注释1] 说遇罗克暗杀毛主席并非空穴来风，手榴弹就是物证。文革武斗分子也有手榴弹，但那是为了保卫毛主席，性质完全不同。有人说遇罗克的言论惹了大祸，这是对的。遇罗克的反革命言论直接改变了手榴弹这个物证的性质。这不奇怪，伊拉克总统萨达姆手里也有武器，而萨达姆是否支持美国的态度，就是决定他生死的关键。萨达姆亲美的时候，美国非但不会收缴他的武器，而且还会支持他打两伊战争，CIA还会给他化学武器去杀库尔德人。但是萨达姆只要一旦反美，连两个儿子都要被杀。萨达姆的一家人，连同被美军飞机炸死的伊拉克平民百姓才是真正“因言论获罪”，遇罗克算不了什么。 <br />
<br />
　　按照所谓现代民主社会的观点，遇罗克公开发表反政府言论，再加上私藏手榴弹也是违法。比如说在美国，一个人公开批评美国反恐怖政策，又私藏手榴弹，被怀疑暗杀布什总统，这种人FBI一定不会放过。手榴弹与普通枪支不同，如果私藏枪支，还可以找借口说是自卫用，而手榴弹则完全是攻击性武器。遇罗克这种反革命暴徒的死，绝对算不上什么“被害”，也算不上“烈士”。特别是，遇罗克的弟弟遇罗文叛逃美国后公开投靠反华组织，逢共必反，发表大量反动文章从各角度对中国共产党破口大骂。所以完全无必要给遇罗克翻案。 <br />
<br />
　　再说张志新，张志新完全是按照某种需要制造出来的典型。原《光明日报》驻辽宁记者站记者、张志新冤案系列报导者陈禹山等人对张志新的本来面目遮遮掩掩，1979年秋，张志新的妹妹张志勤就曾经在北京曾追问陈禹山：为什么辽宁有些人说我姐姐有“作风”问题？ 陈对此避而未答。当时关于张志新的报道中，已经披露了张写给丈夫和女儿的遗言：我对不起你们……这句话是双关的，既有自己遭罪牵连家庭的意思，也有一种夫妻感情上的内疚。但那时陈禹山觉得对张志新的私人生活不应过多暴露，因为那时张志新已经被某些人指定为揭批文革的英雄人物了。 <br />
<br />
　　而实际上，张志新确有“第三者”，以至于20年后陈禹山才终于说出实情，陈回忆说，当时辽宁一些人传言张志新“生活作风”有问题，其实是有所依据的。陈在调看张志新案卷时见到了张本人自白：她确实同沈阳一位文艺界人士有婚外恋。陈说，当时看到这一案卷时，已经采访过张的丈夫曾真，了解到曾真当年体弱多病，可以想见，在夫妻性生活方面，身体健康的张可能处在一定程度的性饥渴状态，加上张天性喜爱文艺，与情趣相投的艺术家产生婚外恋，是可以理解的。 <br />
<br />
　　实际上陈禹山至今仍然没有说出全部真相。据现居住在美国，曾经于1979、1980年两次采访张志新事件的原《人民日报》记者刘宾雁先生回忆，张志新的婚外恋对象不止一人，据刘宾雁回忆，张志新对于党校一位教员（不是文艺界人）感情很深，文革前已遭批评，关系中断，但张志新还把那人的照片偷偷地缝到军大衣的领子中，被捕后，自责对不起丈夫，这才告知其夫取出烧掉。张志新入狱后其夫一直未去探监，直到她提出离婚时才去见了一面，给张志新刺激很大。[注释2] <br />
<br />
　　众所周知，文革时期是中国历史上空前绝后的没有妓女的社会，是道德高尚的纯洁社会。张志新靡烂、放荡的生活方式自然会与社会产生摩擦，张志新的死不能单纯归结于政治原因，这里面有复杂的社会背景，是张志新堕落的个人道德观与纯洁的社会道德观念碰撞造成的。 对张志新的判决过程并未违反法律程序，到最后执行死刑之前经过了多次改判、反复。陈禹山回忆说，张志新曾两次被判死刑。第一次是1970年5月14日。盘锦地区革命委员会人民保卫组判处张志新死刑，立即执行。案件呈送到了沈阳市中级人民法院。据沈阳市中级人民法院当时的办案人员回忆介绍：张志新1969年逮捕后，原打算只要她认罪，根据策反方针，判几年刑就行了。可是她拒不认罪，于是被判死刑，立即执行。之后，案件呈交已实行军管的辽宁省高级人民法院。 <br />
<br />
　　辽宁省高级人民法院谢越1979年接受陈禹山采访时回忆：当年省高院军管会把张志新案首先给一处副处长高振忠审。高振忠看了市中院的报告，私下说：毛主席指示“一个不杀，大部不抓”，张志新是动口不动手的，不搞破坏的。在组织会议上，党员在党的会议上发表自己的看法，构成犯罪吗？要判张志新劳改。他考虑判两年以上有期徒刑，并征求他的上级主管单位负责人意见，得到认可。高振忠在誊抄审判意见稿时，把刑期改为15年。 但是张志新在关押期间拒不反省，拒绝接受改造，而且还在监狱里笔伐谩骂监狱长。喊出“打倒毛泽东”的反动口号。张志新的案子惊动了辽宁省最高负责人、沈阳军区司令员陈锡联，陈发话：留个活口，当反面教员，不杀为好。于是，改判张志新无期徒刑，投入沈阳监狱强迫劳动改造。 <br />
<br />
　　1973年11月16日，张志新与其他犯人参加一次“批林批孔”大会，当报告人批判林彪推行“极右路线”时，张志新站起来喊：“中共极右路线的总根子是毛泽东”。引起大哗，因此，张志新被打成“现行反革命分子”被认定“仍顽固坚持反动立场，在劳改当中又构成重新犯罪”，被提请加刑，判处死刑，立即执行。 <br />
<br />
　　1975年2月26日，中共辽宁省常委召开扩大会议，专门审批张志新案件。出席这次会议的有17人。会上，蔡文林作了《关于现行反革命犯张志新的案情报告》。 <br />
<br />
　　1975年2月27日。辽宁省高级人民法院遵照省委常委扩大会议决定，给沈阳市中级人民法院下发文件：你院报省审批的张志新现行反革命一案，于1975年2月26日经省批准处张犯死刑，立即执行。 <br />
<br />
　　从这一系列过程可以看出，1969年8月张志新被捕开始，到1975年张志新被执行死刑，前后拖了6年之久。大家知道文革是1976年结束的，张志新只要再稍微收敛一下，完全有出狱平反的可能。可是她偏偏要“笔伐监狱长”，喊“打倒毛泽东”的口号。在牢房里喊口号还不够，还要跑到“批林批孔”大会上喊。当年重庆渣滓洞白公馆的共产党员也没有这么干的，在我看来，张志新的行为形同自杀。文革中被打成右派蹲监狱的何止张志新一人，那么为什么别人可以出狱平反活到90多岁，而张志新却偏偏要迫不及待地自杀？根本原因还在张志新自己。事实上，张志新的确有自杀纪录，据南方周末报道记载，“1969年1月9日。张志新写下遗书，准备自杀。被发现后，严加监视，并召开批斗会，批判她‘以死向党示威对抗运动”。 <br />
<br />
　　张志新的婚外性行为被揭发后，精神受到了极大打击，自觉无颜见丈夫孩子。被捕后一年，丈夫曾真提出与张志新离婚。此时，张志新已经报定了自杀的决心，她在1969年1月5日给丈夫的信中说：“以前千错万错，如果不能饶恕，我愿接受最严厉的惩罚，毫无怨言。我懂得了革命，决心要为革命献出一切”！[注释3] <br />
<br />
　　婚姻生活和政治失意的双重精神打击，使张志新丧失了活下去的欲望。她故意笔法监狱长，呼喊打倒毛泽东的口号，强行给自己戴“顽固不化反革命分子”的帽子。甚至在辽宁省最高负责人、沈阳军区司令员陈锡联发话“留个活口，当反面教员，不杀为好”之后仍旧顽固对抗，以此来逼迫执法机关杀了自己。所有能想到的对抗手段张志新都用上了，她“用窝窝头沾着月经血吃，在床上大小便”。[注释4]。 <br />
<br />
　　有人说张志新当时已经精神失常，我比较欢迎这种推测，因为既然精神失常，那么她就不是勇于直言的革命斗士，她的言论就是病症，是精神病患者的胡言乱语。 <br />
<br />
　　从任何角度论证，张志新的死都算不上“迫害、屠杀”，用张志新的个案否定文革毫无说服力。1979年中共辽宁省委追授张志新为“革命烈士”也是非常荒谬。张志新无非是右派上台后为了否定文革故意编造的贞节牌坊罢了。 <br />
<br />
　　张志新作为一名共产党员应该置生死于度外。对党员来说，把自己的一切献给党是理所应当的事情。“保守党的秘密，随时准备牺牲个人的一切”这是每一名共产党员在入党宣誓时都要说的话。张志新的死完全是共产党内部的事情，张志新死得其所，无怨无悔。轮得着反共分子、台独分子们替张志新鸣冤叫屈吗？ <br />
<br />
　　另外，还有一个问题需要澄清。公开宣传张志新一案是胡耀邦批准的。《一份血写的报告》见报时，并没有“割喉管”的说法。后来流传的所谓“割喉管”的说法完全是谎言。更有甚者，海外的反共组织、台独组织为了进一步诬蔑文革，起初说是张志新一个人被割喉管，后来发展到三十多人，三百多人，无名无姓，后来又说什么张志新被人强奸，完全凭空捏造。 反共台独组织最拿手的就是编造谎言，招摇撞骗，混淆视听，用这些“伪历史”来吓唬台湾人民，好像台湾回归了，台湾人民就会面临被中共割喉管的危险。这种东西在国外看多了自然见怪不怪了。 <br />
<br />
　　文革中有没有犯罪现象？当然有。但是这并不能否定文革。因为犯罪分子有可能支持文革，也有可能不支持文革。有些人本来就是文革的镇压对象，他们犯罪当然不能把责任推到文革头上。就像现在很多人打着改革的旗号干着违法乱纪的事情，你是不是也可以收集起来将改革全盘否定呢？显然不客观。 <br />
<br />
　　文革时期不准反对毛泽东也很正常，因为毛主席代表了广大人民的利益，谁反对毛主席谁就会受到老百姓的制裁。这些全是老百姓自发的行为，毛泽东从未下达过“谁反对我你们就必须打倒谁”这样的指示。根本找不到。 <br />
<br />
　　民运分子常说：任何民主都不能脱离法律约束。而《毛主席语录》在文革期间就是法律，这是几乎所有人都要受到约束的法律，人民就在这样的法律的约束下进行文革。法律总是代表统治阶级利益的，不服气不行，文革期间的统治阶级是无产阶级，《毛主席语录》就是无产阶级的法。遇罗克、张志新反对《毛主席语录》就是违法。 <br />
<br />
　　注释：<br />
<br />
　　[1]详情请见中国文联出版公司《遇罗克・遗作与回忆》一书，徐晓、丁东、徐友渔编 <br />
<br />
　　[2]摘自：《张志新的凄然清泪》原载香港“开放”杂志，作者：茉莉 <br />
<br />
　　[3]摘自：《张志新冤案还有新的秘密》原载《南方周末》，作者：陈少京<br />
<br />
　　[4]摘自：《张志新冤案还有秘密》原载《南方周末》，作者：朱建国 <br />
<br />
　　三、遇罗克和张志新被杀主要他们是造反派 <br />
<br />
　　类似的言论在文革期间很多，如果这个人是保守派的，就不会有问题。武汉水利学院曾经有一个保守派学生，说毛泽东发动文革是搞权力斗争，造反派还为此举行了游行抗议，这个人一点事没有。另外在武汉国棉叁厂有一个叫顾俊泉的，1967年720之后是厂斗批改领导小组组长，当时武汉720之后大家从严酷的现实中解脱出来，许多人在面临生死威胁的背景下解脱出来，因此对毛泽东的感情迅速升华，作为这个情绪的体现，厂里工人一致要给毛主席祝寿，这个厂决定绣一幅巨幅的毛主席去安源的画像，在进行过程中，一些保守派的人特意事事去请示，并记录和曲解他的意见，其中他的一条意见是毛主席的面部在背光的一面，要用阴线体现层次感，这样就被说成是攻击毛主席也有阴暗面，结果在1970年被抓起来关了四年。如果遇罗克和张志新不是与本地当权派作对的造反派，他们的言论根本就不会被追究。而他们被执行死刑，还有另外的时代背景，遇罗克被杀的大背景，是一打叁反运动中在造反派中抓坏人，后来在1971年对造反派镇压的升级，这是“批极左”运动的结果。1971年武汉枪毙了13个有点问题的造反派，相比较而言，遇罗克的问题要严重得多，比如武汉有一个被枪毙的人，是因为他本人在叁年困难时期做过生意等这样的“投机倒把”问题也构成死刑，当时的大背景是就要严厉镇压造反派；1975年对造反派处理趋向严厉的背景是dxp主持搞“治理整顿”，要求各地对造反派的打击要“从重从快从严”，能抓的就抓，要流放的就赶紧流放，武汉当时就判处了几个有期徒刑，其中一个名叫武齐华的就被抓，大概一个多月之后死于监狱里，另外有几个被判刑。<br />
<br />
　　其实这些问题都是特定时代的政治问题，结果被今天的一帮子文人歪曲为一个单纯的技术性的法律问题。遇罗克张志新他们当时都面临着一个强大的敌对的政治派别，他们本身属于另外一个政治派别，因此把他们治罪，并不是人们在认识或者对法律把握或者理解上出了什么问题，而是实实在在的政治派别斗争的结果，他们因为自身有各种问题因而被对立面抓住不放，抓对立阵营的坏人是一派打击另外一派的常见手段，而张志新在弱势条件下还不知道自保，其结果可想而知。<br />
<br />
　　辽宁情况看起来比武汉好，辽宁当权派看来与造反派的对立还不是那么严重。去掉这样的时代背景把问题技术化，是纯粹的谣言和别有用心。<br />
<br />
附文：<br />
<br />
　　她第一次被判刑前就已经精神崩溃了，她精神崩溃的原因，就是因为不正当男女关系被暴光，那个时代这种事是最见不得人的，俺记得八十年代初期地方铁路有一对男女职工在苗圃地办事，被人发现后，俩人就再没有出来，他们双双自杀在林子里了，那时侯的道德风尚的确象一把无形的刀，张知心的隐私败露了，她政治前途没有了，作普通人的条件也没有了，她怎么极端排斥丑恶的人群中生活？她只有选择死亡，但当时就是不判她死刑，她于是千方百计求死，用馒头蘸自己的月经吃……，一个极端崇尚高尚的社会如何能任由她如此生不如死地活着，经过无数次的反复最后无奈选择了了结她不是一个求生的精神病，而是一个为求一死的精神病 <br />
<br />
　　1969年1月5日，张志新给曾真写了一封诀别信。<br />
<br />
曾真：<br />
<br />
　　结婚14年生下了一男一女，我没有也无力完成自己的义务，希望你很好的抚养下一代，对林林要耐心，女孩子每长一年事就更多，要很好爱护她。叫她不要早婚，妈妈对不起他们。春节好好照顾。过去自己修养不好，打骂过孩子，让她别往心里去！好好学习。锻炼身体。改正“没有坚持精神”的缺点。让她好好照顾小弟弟，不要伤心，要坚强。 <br />
　　十几年我对你没疼没爱，犯过的错误已结束了。彻底把我忘却，重新开始新的生活！原来为你买东西的那笔钱，是我平时结余的，打算为父母办理丧事用的，如能积起可交我母亲治病用。也是最后一次尽孝！不过不要告诉他们，这会使他们受刺激犯病(你尽可能这两三个月每月给他们寄15元吧！也可不寄，叫志勤寄)。平时多注意身体！为了革命多照顾自己吧！我没给父母写信。如果沈阳家里没人照看，你可写信去和母亲商量是否把孩子放(天)津！不过我考虑，他们若身体不好，困难会大些。如若可能，还请何姥来照看，工资稍少些可减轻负担！总之担子都是你的了。对孩子要耐心！对不起你。十几年辜负了党的培养！一个人不管是生或死只要是为了革命就是有意义的！<br />
　　我懂得了革命，决心要为革命献出一切！<br />
　　以前千错万错，如果不能饶恕，我愿接受最严厉的惩罚，毫无怨言。<br />
　　真正的革命事业永远是兴旺的蒸蒸日上的。为盘锦的美好未来欢呼！再次欢呼这个胜利的前途！愿为美好的未来添点出点力。但有没有这种可能，确不是能由我所决定的，革命能否容纳，党和人民决定。怎么定我怎么领。<br />
　　中国共产党万岁！<br />
　　伟大的祖国万岁！<br />
　　毛主席万岁！<br />
<br />
　　志新<br />
<br />
　　1969年1月5日晚<br />
