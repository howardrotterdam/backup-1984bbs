早年的情形如何?在过去的100多年间里曾有多篇描述胃内存在螺旋状细菌的报道,但都不受重视。例如,1940年Freedburg就发现了几例并作了一短篇报道。但1954年Palmer进行的专门研究却否定了胃内有这种细菌存在。医学教科书的教条也明确得很,正常胃内无细菌生长,胃内的酸环境会将吞入的微生物迅速杀死,只有胃萎缩、溃疡坏死碎屑中才会有微生物生长,可能属继发性感染,且大凡是真菌。泰安市中医二院胃镜室陶可胜<br />
<br />
20世纪70年代以前,很难得到高质量的胃活检标本。大多数胃标本来自外科手术、甚至是尸体解剖,这类标本的粘膜多已发生自溶,很难看到精细的粘膜病理改变,若有细菌也早已消失了。那时病理检查很少见到慢性胃炎,慢性胃炎与溃疡、胃癌显然也没什么关系。那时的胃炎分类方案除了恶性贫血相关的萎缩性胃炎及某些急性胃炎外,基本没有什么临床适用价值。恶性贫血是学校必讲的课程,但临床却很少见。很多活检标本病理显示慢性胃炎伴不同程度的灶性萎缩未被认识有什么临床重要性。对于我,这一切在70年代后突然发生了改变。<br />
<br />
纤维内镜的问世,使得迅速固定良好的胃肠粘膜活检标本成了最常见到的病理活检标本之一。基于这类标本病理研究,促成了1972年Richard Whiehead的胃炎分类。Whiehead的分类很复杂,但十分符合逻辑,他用常见、易识别和易定量的指标将活检病理进行描述,例如部位、深度、类型、炎症严重程度、胃腺萎缩、肠化等。Whiehead所指胃炎“活动”,即粘膜上皮出现白细胞侵润灶,其实在胃粘膜活检病理中经常见到,但一直被忽略。我的感觉是Whiehead分类方案易学好用,结果稳定。<br />
<br />
1979年4月,在一份胃粘膜活检标本常规H-E病理检查时,除了重度活动性慢性胃炎的病变外,我发现粘膜表面有一条奇怪的蓝线,转成高倍镜后观察,发现是无数杆菌紧粘着胃上皮。我觉得这很不寻常,因此拿给同事们看。但是他们都说看不到,这使我十分气恼。我就尝试着对切片作Warthin Starry银染色,结果细菌清晰可见,低倍镜下就能看到,数量比想象的更多,范围更大。我高兴得不得了。用蜡块标本作电镜检查同样也清楚看到这种大量的细菌,形态与弯曲菌(Campylobacter)相似属细菌。这下子,同事们也肯定这种细菌的存在,但重要性则是另外一回事。<br />
<br />
我继续对所有新来的胃活检标检查这种细菌,但并不真的指望发现它们。出乎意料的是,它们经常出现,不过数量通常没有第一例多、常呈灶状分布。很快我发现这种细菌常出现在组织学能诊断的慢性胃炎标本中,特别是Whiehead所指的活动性胃炎,也就是有中性粒细胞浸润的胃炎,而且近一半胃窦粘膜标本能见到这种细菌。在这一时期,只要发现了这种细菌,我都在临床病理报告单上注明。不过,我想没人会认为它们有点什么重要性。<br />
<br />
我觉得这种细菌和慢性胃炎关系是很密切的。但除了我妻子以外,我几乎没得到其它人的支持。许多人认为这是垃圾。也难怪,这个发现不符合那个时代的医学理念,当时认为正常胃是无菌的。胃酸分泌会将吞入的细菌迅速杀灭。酵母菌通常生长在溃疡底部的坏死碎屑中,肯定是继发于溃疡。我得到的最好评论是“即使细菌生长在那里,它们也是继发于胃炎”。而对于我来说,这是错误的。细菌与炎症的关系以及细菌存在时不寻常的特征,提示感染是原发的。然而,想证明这一点是很困难的。我想应该设计一个阴性对照试验会对证实这种想法有帮助。我从未接触过临床,想从临床医师里得到一我些帮助,取一些胃镜下看起来是正常的标本,但医师们不同意,他们告诉我“只在需要的地方钳取标本”。因此,他们的活检标本可以取自胃的任何一个“需要的地方”,也就是说为了检查溃疡或肿瘤性质而活检,而且都是取自病灶的边缘。这些标本很难区分与细菌有关的改变和继发病变。医师们根本就没打算活检标本要找细菌,将胃活检标本送细菌培养的想法更是荒唐。“如果真有这种细菌,为什么以前没人报道?”我不知道为什么我以前从未看到过它们,所以这个问题很难回答。后来才发现了许多以前的有关这种细菌的报道,包括100年以前一些美妙的描述,只是以前没有人进一步研究它们罢了。当然,这是后话。<br />
<br />
没有医师的支持,我只好从病理档案中寻找报告为正常的胃活检标本蜡块,看看这些正常胃粘膜有没有这种细菌。没想到竟如此因难。大部分报告为正常的标本大部分是来自胃体,病理变化轻微,相伴的胃窦则表现为活动性胃炎,并且能见到细菌。我错误地认为这种细菌只感染胃窦。档案并没有将胃粘膜活检部位分列,很难找到报告标明胃窦粘膜的病例。但最终还是找到了20例。在这20例中,发现一例有细菌,而这例病人我重新复查病理后发现是中度胃炎,而没有细菌的另外19例的确是正常。将有细菌的那例病理片请原报告人重新阅片,他复习后认,原来看错了,应该是胃炎。与此同时,临床送检的标中,几乎一半胃窦粘膜能发现细菌,只要是有细菌的粘膜就同时存在慢性胃炎,大部份属活动性胃炎,许多病例还伴有不同程度萎缩、局灶性肠化。而病理正常的胃窦粘膜绝不会有细菌出现。<br />
<br />
1981年,正当我准备发表我的发现时,一位年轻人出现了,他就是Barry Marshall。Barry Marshall是本院消化科注册医生,要求发表一篇科研论文,由于对建议颗题不感兴趣,有人建议他来了解一下我的工作――“一位病理学家正试图将胃炎归因于细菌感染。” Barry Marshall向我了解情况后,对我的工作也不感兴趣,也不相信我的论点。不过还好,他答应给我提供小系列胃粘膜活检标本。按我的要求,活检取自胃窦外观正常的粘膜、一定避开溃疡等明显病变部位。结果证实我的观点,胃炎和感染并非继发于溃疡。Barry Marshall突然对这种细菌表现出极大的兴趣,并从此把一生的精力都投入到对这种细菌的研究中去。至今我还不明白他是怎么突然感兴趣的。<br />
<br />
接下来,我们对100例胃镜检察病人进行正规化的研究。收集资料包括临床症状、标准化胃窦活检作病理检查和细菌培养,以分析细菌、胃炎与临床症状、胃镜表现的关系。结果出乎预料,组织学发现的细菌及其胃炎与大多数临床症状、胃镜表现几乎无关。临床症状方面仅口臭及打膈和细菌有关,不论胃镜表现如何,这些做胃镜的病人多数有上腹痛。胃镜诊断的胃炎与组织学诊断的胃炎极不吻合。细菌培养方面,由于这种细菌很象弯曲菌,我们就按弯曲菌分离培养法进行培养,但没有象粪便分离弯曲菌那样在培养基中加抗生素。起初,培养总是失败,直到复活节才发现几例阳性。彻底检查分析原因后,发现问题可能出在培养箱漏气。将培养箱修补后,培养从此变得非常可靠。最后,Barry Marshall汇总胃镜检查结果,我们惊奇地发现,所有十二指肠溃疡病人胃内都有这种细菌!<br />
<br />
所有这发现都十分有趣,但临床医生还是不信服。1983年我在&lt;&lt;柳叶刀&gt;&gt;杂志上发表了我所工作的短篇报道,文后附上Barry Marshall有关我俩共同研究结果的信稿。随后,Barry Marshall在布鲁赛尔弯曲菌大会上报告了我们的发现,引起Martin Skirro的重视。Martin Skirro是英国弯曲菌研究权威。这真是一个幸运的机会。因为会后我们将研究结果以论著形式向&lt;&lt;柳叶刀&gt;&gt;投稿。但投稿数月未见回音,因为编辑部未获得一位审稿人的同意,这意味着论文不可能发表。最后,我们与Martin Skirro联系。他在他的研究室作一点与我们相似的工作,并将结果呈送&lt;&lt;柳叶刀&gt;&gt;杂志。1984年4月,我们的论文终于一字不改地发表了。<br />
<br />
我们继续对这种细菌进行研究,早期研究涉及诊断、治疗、证据三方面。诊断方面的许多试验都是Barry Marshall提出的,如治疗前血清学试验、呼气试验、快速尿素酶试验(CLOtest)、组织学、涂片、培养等。在治疗方面,Barry Marshall尝试铋剂和抗生素。关于铋剂是Barry Marshall在一旧版Willian Osler内科学教材得到的启示,结果的确有效。至于证据方面,Barry Marshall和新西兰的Arthur Morris医生均使用确定致病菌的Koch原则:所有病人都能分离出同样的细菌,培养细菌能引起相同的病变。前者是明确肯定的,后一条则是由这两位可敬的医生自愿试验才证实,两位医生胃镜活检检查细菌均阴性,在服食培养的细菌后,都发生了胃炎,细菌检查阳性。Barry Marshall医生的急性胃炎很重,但很快自己给自己治好了。Arthur Morris医生则不断给我寄来胃镜活检标本,检查一直存在细菌和慢性胃炎,费了好几年工夫才治好。<br />
<br />
一些早期研究病例显示存在不同的临床类型。如NSAI类药物可以引起十二指肠溃病,但并不意味着细菌不起作用。我妻子就是早期研究病例中的一员,她患有关节炎需要服NSAI类药物,服药后关节炎好了,但又出现胃痛;停用NSAI类药物,胃痛好了,但关节炎又复发,如此反复。我带她看医生,Barry Marshall检查证实有细菌,将细菌杀灭后继续用NSAI类药物,胃就不痛了。而且妻子治好后发觉我有口臭,检查竟然也有细菌,但没有一点胃部不适,根除细菌后妻子就不感觉我有口臭了。<br />
<br />
我们还作了双盲、抗菌治疗十二指肠溃疡的研究。病人在接受抑酸治疗的基础上,加上抗细菌药或安慰剂。结果发现,无抗菌治疗的溃疡很快复发,而根除细菌的溃疡则很少复发。同时也十分清楚地显示细菌在组织学胃炎的作用。根除细菌后,组织学显示的活动性炎症迅速消退。其它变化则很慢和不完全,解剖学异常如萎缩、化生、纤维化则少有改变。若细菌存在,则胃炎不变。<br />
自我们的研究报道后,全世界掀起了的研究热潮。消化科研究它与消化病的关系,非消化科就研究它与各种非消化病的关系,动物界也发现各种螺杆菌。公共卫生问题备受关注,制药公司投巨资开发有效药物,&lt;&lt; 螺杆菌&gt;&gt;杂志应运而生,世界性幽门螺杆菌大会定期召开, 幽门螺杆菌研究论文不计其数。<br />
人们常问我,作为幽门螺杆菌的发现者对我个人意味着什么?我很难回答。不过,这一发现改变了我的生活。我继续当病理学顾问,许多研究工作和写作都有是在下班后完成的。我的妻子Win是一位执业医师和精神病学家,当没人相信我的时候,她坚定地支持和帮助我。后来,这一发现被海外接受,我们应邀到世界各地讲学和旅行。Win对我的支持获得了某种回报:她总喜欢旅行。</div>

							
							
							
							
