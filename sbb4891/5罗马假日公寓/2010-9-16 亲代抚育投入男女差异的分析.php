																											<div id="postmessage_833215" class="t_msgfont">原标题为：生完小孩后，是否要父子间做亲子鉴定<br />
为减少误会歧义，改成现在这个<br />
<br />
摘录几段<br />
<br />
<br />
<br />
<br />
在动物王国里，对于子女和自己的遗传关系，母亲通常都百分百地“确信”。这里有必要把“确信”二字用引号括起来是因为她们无需有意识地确认自己的母亲身份。当雌性生产或者排卵时，毫无疑问她们的后代将含有她的50%的基因。雄性却不可能“确信”。父子关系的不确定性问题意味着，从雄性的角度来看，总是有可能有其他雄性使雌性的卵子受精。<br />
<br />
一项对多种鱼类和两栖类动物的研究发现，有86%的体内射精物种是有雌性承担大量的抚育工作（Gross &amp; Shine，1981）。相比之下，对于鱼类和两栖类等体外受精物种来说，只有30%是雌性比雄性提供更多的亲代投资，余下的70%是雄性比雌性对亲代抚育投入更多。<br />
<br />
对于体内受精的物种来说，比起体外受精物种，雄性确定哪个精子使卵子受精的途径更少，因此它们的父子关系的不确定性就更大。当受精在体外发生时，雄性动物有足够的线索来确定自己的父亲身份，例如目击自己的镜子包裹住雌性的卵子，或者探测到卵子内有其他雄性精子的存在。<br />
<br />
<br />
<br />
父子关系的不确定性还不足以阻止亲代抚育的进化。但是，相对于母亲来说，父亲为子女投资显得不那么有利可图。在父子关系不确定的情况下，对于每份相同的亲代投资，母亲获得的回报就比父亲更多，因为“父亲”的投资的一小部分可能就浪费在其他人的子女身上。相比之下，母亲的亲代投资是百分之百地针对自己的孩子的。总之，尽管父子关系的不确定并没有阻止雄性的亲代投资机制的进化，却仍是雌性比雄性对后代投入更多的普遍倾向的一个可能的原因。<br />
