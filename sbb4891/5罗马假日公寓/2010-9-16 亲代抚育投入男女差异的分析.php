&nbsp; &nbsp; 相反，那些聪明、有智慧、有才华，但家庭出生不好，父母是穷苦老百姓的人，由于社会的上下流通渠道被世袭制所阻断，因而其能力和才华没有地方发挥，反而变成了很无用的东西，是不务正业。<br />
&nbsp; &nbsp; 社会很混乱，权力被流氓掌握了，财富也被流氓掌握了，美女当然也不能例外。流氓垄断了几乎所有最重要的社会资源，不仅如此，他们还创立了与流氓社会相适应的流氓道德、流氓文化、流氓制度，让我们心甘情愿的接受流氓的统治，认为流氓的统治是合情合理的。<br />
<br />
&nbsp; &nbsp; 有人可能不承认，认为自己不会这么笨。是吗？如果你出生在古代，你不是认为某家王朝（比如，汉朝是刘家王朝、唐朝是李家王朝、明朝是朱家王朝）是很正常的吗？你不是认为皇位父死子继是天经地义的吗？异姓争皇位是大逆不道；如果你出生在现代，你不是认为个人掌握亿万巨富是正常现象吗？富豪们将财产传给他们的子女是天经地义的吗？在这些问题中，有多少人能回答“否”？你看，谁说我们没有心甘情愿的接受流氓的统治？<br />
<br />
&nbsp; &nbsp; 美女就是这样给流氓搞上的。最初的美女都是属于有能的、有德的，能获得众人认可和支持的人，无能、无德的人甚至不享有交配权。<br />
<br />
&nbsp; &nbsp; 可是由于血缘亲情的影响，大约从父系氏族社会开始，有能力的、掌握权力和财富的人，开始将他们手中的权力和财富世袭给其子女。然而由于人的能力不具有世袭性，于是社会开始变得混乱，否则我会认为中国古代的西周社会是最完美的社会，因为那时的社会，权力和财富都是严格按照血缘的尊贵从上到下一代一代世袭的。<br />
<br />
&nbsp; &nbsp; 这样，权力和财富逐渐沦落到无能的、无德的流氓手里。流氓又改变了人们对能力的评价标准，所谓有能力就是看其是否掌有了权力和金钱，另外，女人的天性也需要男人有社会资源（权力和金钱）能照顾自己。真正有能力的人反而被压制了，他们的才能无法发挥，变成了很无用、不务正业的东西，他们也没有社会资源来养活美女、照顾美女。<br />
<br />
&nbsp; &nbsp; 于是美女开始喜欢上了流氓，流氓在他们垄断了权力和财富之后，又心满意足的垄断了美女。社会还混乱，我们还需多努力，彻底消灭世袭制和变相世袭制，建立能力治国的社会，美女才能摆脱流氓的控制，重新回到有能力、有才华的人的怀抱。<br />
<br />
&nbsp; &nbsp; 值得庆幸的是，为了对付世袭制和变相世袭制，人类已经发明了两种非常有效的能力治国的方法：民主选举制和科举制。<br />
<br />
&nbsp; &nbsp; 民主选举制是西方人的发明，它依据的基本原理就是少数服从多数，多数人的决定通常要比少数人的决定更正确；科举制是中国人的创造，它依据的基本原理是治理国家是需要非常丰富的专业知识的，因此就必须培养治国者这方面的知识，并通过考试的方法选拔最优秀的人才。<br />
<br />
&nbsp; &nbsp; 我们以后要做的事情就是将这两种方法最合理的结合起来，另外就是将这两种方法从单纯的政治领域推广的经济领域。一旦政治领域（权力）和经济领域（财富）的资源都被真正的人才所掌握，美女想不回到有才能的人怀抱都不可能。 <br />
