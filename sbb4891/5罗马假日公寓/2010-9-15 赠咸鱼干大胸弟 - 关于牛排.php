													<h2>赠咸鱼干大胸弟 - 关于牛排</h2>
						
						
																											<div id="postmessage_830426" class="t_msgfont">牛排我不喜欢吃NY STEAK。 就偏爱RIB EYE。因为比较肥， 中间的油脂化了的时候很香很好吃。<br />
黄阿狗说得对，牛排质量很重要，不好的牛排怎么做都不行，要不川菜得了，拿调料盖一盖。。。<br />
<br />
牛排质量好的前提下，怎么吃？盐与胡椒足矣。什么A1烧烤酱我从来不用，什么加拿大烤牛排特别调料我也不用，什么韩国牛排，什么台塑牛排我都不吃，而且牛排馆里的也少有做得比自己做的好的。很少。原因很简单，牛排馆里多少人吃牛排？大厨怎么掌握生熟？什么时候给你和你朋友凑齐了一起上菜？来不及！<br />
<br />
好牛排要趁热，要热盘子，而且当时就上，这么好的服务谁有？只有自己有! 所以我烤牛排，第一邀请的朋友不超过四人。因为忙不过来，第二牛排要趁热，只能出来的三五分钟内吃，谁要顾着聊天不吃，我跟他急。第三我只烤RARE一种，烤MEDIUM的我都心疼，可惜了牛排，要吃WELL的我一律拿香肠去应付，WELL了还吃什么牛排啊，吃汉堡香肠得了。<br />
<br />
另外，牛排不能煎，要烤。我不够奢华，有奢华的专门买现宰的牛排，FEDEX特快空运，第二天早上八点前送到的，两磅，好像一百多美金。我买不起。就是平时去店里买RIB EYE。看一下脂肪分布比较均匀的就好，厚度，不能低于一英寸2.54厘米。<br />
<br />
烤的时候太简单了，炭火或者煤气火都行，要闷的，不能明火，冰箱冷藏的，决不能冷冻，冷冻血汁就没了！冰箱拿出来的温度下，一面抹盐于胡椒颗粒，五分钟，翻面，抹盐，胡椒，六分钟，时间长短要稍微调一下，大概就是这个概念。然后呢？完成了，吃吧。<br />
<br />
没别的诀窍，就是盐与胡椒。 吃的时候感觉血汁，JUICY的感觉，就对了。好吃的牛排就是这个味道，别的什么都不用。</div>

							
							
							
							
