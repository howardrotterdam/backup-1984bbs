													<h2>创业前的准备</h2>
						
						
																											<div id="postmessage_659125" class="t_msgfont">我相信有志创业的朋友如我当时一样，已近看过足够多的书，混过很多创业论坛。也对自己想要进入的行业做了自认为足够的分析了解。也知道了一将成名万骨枯的创业残酷性，这都是必须做的功课。只是这时都认为自己是将别人是骨罢了。<br />
<br />
为之环顾四周为之踌躇满志是那个阶段的典型心态，一副世界尽在掌握的模样。这是最美好的阶段，因为还没有开始。就如同没有上床前的男女一样，期待着最美好的可能性。<br />
<br />
如果可能，尽量延长这个阶段。如同前戏，足够才美好――哪怕最终如在东瀛做王的虬髯客因为无休止的扒开妃子们的衣服到最后忘了自己为什么要解开这些衣服――最少这样会留下美好的想象，而非更残忍的事实――丫原来不举。<br />
<br />
但聊得再好也是要上床的，过长的前戏会让你忘了你仅仅是想放一炮并而已不是想结婚。作为一个负责的男人，你应该准备一个好的套套，冈本、杜蕾斯一类的，不要用诸如男子汉一类充满自我暗示的玩意来糊弄自己和姑娘。你还应该准备好开房的钱，和一顿不错晚餐的钱。<br />
<br />
同样的，创业前应该准备更多。<br />
<br />
第一个准备就是做好失败的准备，甚至创业初期的一切准备都是围绕“如何避免失败”展开的，而非围绕“如何成功”展开，那些创业赚大钱后换老婆（更大可能只是换换GF而已）买车子买房子搞女子的幻想请放到赚到钱后再想。还在是左手好还是右手好的年代想今天是和林JJ宵夜还是和张MM做个JB毛用。<br />
<br />
如同再好的套套也可能中标一样，再好的项目再好的团队再好的产品也可能失败。一定要做好失败的准备。<br />
<br />
如果你长期坚持做之前观其色，闻其味，摸其肤，并坚持披挂上阵，最终仍旧中标的话，请接受这就是你的代价。那些惊慌失措路边找老中医老军医的SB行为仅仅来源于不想接受事实不敢面对现实而已。一个坦然接受命运的男人，会从容的去正规专科医院挂专家门诊，哪怕付出凌晨2点去排队的代价。<br />
<br />
我们能看到所有的成功的故事，一定要牢记那只是个故事。虽然你想成为新的故事，也认为自己能成为新的故事。但记住这仅仅是个意愿，而不是现实。事实就是如果你成功了，你才可能是新的故事，如果没成，你就P都不是，你的经历和想法就只对自己有价值。<br />
<br />
大家都经常看到贼吃肉，选择性的看不到贼挨打。当然贼也不会让看到。<br />
<br />
基于一定要做好失败准备的基础，第二个建议就是：时间和精力上要孤注一掷，但财务上一定不要孤注一掷，给自己留有退路。这不是泄气，这是理性的做法。<br />
<br />
对初次创业的人而言，全力做好一件事是必须的，没有任何选择。但这仅仅是指时间和精力上的全力，如果你和你的伙伴做不到的话，那就不要启动项目。但财务上要留有退路。比如，不要把自己全部的资金投入创业，如果一定要给个比例，不要超过50%。当然越少越好。如果你投入的资金比例占你你可支配资金总额比例越低，你的回旋余地越大，可能性也越大。在投入足够低比例资金情况下都能成功，说明你的状况非常好，而且会越来越好。<br />
<br />
而且这个建议是公平的。假设你有团队，这个应该是对团队里所有人的建议。<br />
<br />
我们时常被破釜沉舟一类的故事所鼓舞，而忘了无论是战争还是商业，这是一个理性的技术活，而非艺术（所谓战争艺术就TM扯蛋，是帮洗脑的一部分，忽悠炮灰的假说。战争是极其严谨的工程+管理学）。激情只会给一个好的开始，而不会给你好的过程，肯定只会给你糟糕的结果。如同激动的恋爱，进入婚姻状态后就只能是理性的生活细节。（广告时间：推荐一个电影《午夜巴塞罗那》）所谓激动在我们漫长的生命中不会占到十万份之一的时间。<br />
<br />
生活就是隐忍的过程，创业也一样。<br />
<br />
总结一下，如下<br />
<br />
1.做好失败的准备<br />
2.创业初期一切准备工作围绕“避免失败”<br />
3.时间和精力必须孤注一掷<br />
4.给自己留下财务上退路<br />
5.分清什么是意愿，什么是现实。牢记：意愿不是现实<br />
6.面对最不堪的现实<br />
<br />
