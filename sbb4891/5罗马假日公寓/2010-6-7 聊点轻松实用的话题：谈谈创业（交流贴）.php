													<h2>要不要创业？</h2>
						
						
																											<div id="postmessage_659124" class="t_msgfont">最近在小组看到不少关于创业的话题，还有不少组员在上学，找工作。关于工作的话题也是另一个热点。本人在创业路上已经挣扎6年，颇有些教训可以分享。谈不上真知灼见，确实是声声泪字字血。<br />
<br />
需要申明的下面的文字是有语境的，脱离这个语境就未必有效果。<br />
<br />
语境是：<br />
<br />
理想主义者<br />
自由主义者<br />
草根<br />
白手起家<br />
大众消费品<br />
<br />
至于什么是理想主义者，什么是自由主义者，大家自有主张和定义，可以慢慢讨论。<br />
<br />
为什么首先要强调理想主义者呢？因为这几年来遭遇的经历，历经的磨难，和自己浓重的理想主义价值观有极大关系，也就是所谓的“性格决定命运”。<br />
<br />
富二代官二代的创业可能也有困难，就如同上个世纪储安平先生关于民主的“多和少”“有和无”问题的论述一样。我们不讨论为今晚和哪个姑娘上床的烦恼，只交流左手和右手的不同。<br />
<br />
×××××××××××××××××××××××××××××××××××<br />
第一个问题就是，要不要创业？<br />
<br />
创业是典型的看上去很美的事。大家都知道，一个糖果好不好吃，吃了才知道，一个姑娘是不是处女，试了才知道。<br />
<br />
如果现在让我再回到6年前重新做个选择的话，我一定会告诉自己，去找份工作，继续在外企混下去。绝对不会是创业。<br />
<br />
时间已经到了2010年了。中国白领的黄金十年（1995-2005）已经结束，现在是黑领当道的年代（预计为十年。非常好玩的发现，在共产党执政的中国，绝大多数的历史事件都是以10年计算）<br />
<br />
我的判断是，从2006年开始的草根创业开始进入严酷期，成功率大大降低。而且这几年国进民退极其严重，加上外贸经济开始遭遇大滑坡，民营资本、民企发展都开始遇到极大障碍（这也是游资或者民间资本进入楼市的核心原因：原来的渠道风险变大，很难盈利，又严重缺乏新的投资渠道，最后都挤到楼市中去了），何况小资本，甚至零资本的草根。<br />
<br />
我的建议是，这几年不宜创业。能避免就避免，能继续上班就继续上班。这个工作不满意，那就换工作。勿轻言创业。<br />
<br />
如果下定决心了一定要创业，那就继续往下看。<br />
