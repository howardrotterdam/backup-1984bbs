																											<div id="postmessage_797289" class="t_msgfont">一般复习分三轮，第一轮很重要。尽量跟上学校复习进度。<br />
高三很寂寞，很无聊，重要的是坚持（虽然高考是XX）。你是四川的？我好多同学去成都理工上的。本省分数线要低一些吧。不知道你有中意的学校吗？不妨考虑一下本省的。<br />
户口（某些人或许又会说。。。）如果你要打算买户口，乘早行动，办户口很费时间。加上高考报名12月份就陆续开始了。<br />
住宿吗？如果住宿尽量别长时间夜聊，那样容易影响休息。午休也很重要，时间不一定长，但建议午休。<br />
我结合别人和自己经验总结的，一些科目如下（量时间而行，你自己取舍吧），我们英语二卷就是没有听力，其它一卷。我理科的。打定主意复读，就尽力吧。<br />
语文，挑一些参考答案的讲解抄到卷子上，同时动脑记。基础知识不一定都背，但考试遇到的尽量记住。作文，买本作文书，每周抄一篇，尽量记思路。花哨句子我这辈子没戏了，一般都是理论。不行就记点名言，事例什么的，考试时候凑字数用。名言记不住或者这样，写：有人曾经这样说，加上你自己的话。至于时事政治什么的你自己看着办吧，我原来写作文老背叫办公室，因为言论。后来每次写作文总往几个固定事例上靠，就没事了。<br />
数学，单选除了基础知识要扎实。可以用代入特殊值试（强烈推荐），或者排除法。最后一道选择往往选C。填空，实在不会蒙1或0，（1，0几率大）或者二分之一。我某同学每天中午午睡前做一套单选，连做到高考，很有效。大题别头痛。实在不会就摞公式。写点就有可能得分。问题一般有关联性。一个小问不会，或者小问中间有不会的，就忽悠过去，或者跳过去，接着会的写。证明题要是不会，别忘了试试反证法。<br />
英语，（不知道你具体什么水平，30，40分？我同学什么不会，考试蒙也就这个分数。）课内单词要是有好多不会，就先背单词吧。买本天利的那种。听力靠练，没别的。二卷考的是语音，单词记得多对的就多。单选，我一般错一半以上。不过，我英语一般110，130。单选除了考语法，还会考一些日常的，日常的保证对就成。语法单选靠语感吧。做过的卷子保存好，2，3轮复习和高考前看的就是单选错题和作文。完形最起码一周做两篇，不会的单词要查，要记（有个印象就成）。那天没做完形，那天做阅读。阅读先看问题，再读原文。重点要点圈出来。改错一般有一个对的。跟单选一样，2，3轮适当复习做过的改错。单词填空别忘了形式要正确，不然白写了。作文，不一定复杂，但尽量别出错。参考答案作文一定要抄一遍。此外挑一些有代表性的背一背。<br />
文综，除了背。要有自己的答题思路。（历史不如看看袁腾飞视频？呵呵，我不懂）地理历史，尤其地理背图。高考前发的时事热点要仔细看。<br />
多做卷子是很有必要的。所谓的反对题海战术的人，得看看他高一高二平时做了多少题。<br />
最后，情感，我自己认为有个男女朋友很正常。高三有个男女朋友或同学诉说一下苦闷，很合理。不过我是失败例子，当她录取电话来的那一天，，，小学就认识的人，最终形同陌路。<br />
最后，高考是XX，面对XX，我们要坚持，不然浪费时间和精力做什么，除非是XX。<br />
加油吧！</div>

							
							
							
							
