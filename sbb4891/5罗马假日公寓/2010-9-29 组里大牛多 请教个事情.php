Abstract :&nbsp; &nbsp; &nbsp; &nbsp;&nbsp;&nbsp;How do totalitarian and authoritarian regimes maintain political control? One of the key strategies is to create social fear. In the case of the political history of both mainland China and Taiwan in the 1950s, the Chinese Communist Party (CCP) and the Kuomintang (KMT) used various methods, such as political mass movements and rule by the secret police, to carry out state violence to create social fear. Based on a comparison of the state violence of mainland China and Taiwan in the 1950s, this thesis addresses the following questions: How does an authoritarian power stabilize control through state violence? What are the institutions of state violence? How is state violence achieved through psychological factors? A general examination of the political movements and suppression on each side on the Taiwan Strait, as well as specific case studies, i.e., the case of Hu Feng on the mainland and the case of Lei Zhen on Taiwan, reveals the following commonalities: the extension of the attack from the political arena to the social arena and the concomitant transformation of state violence from a political issue into a social issue, extending to every level of society. I also examine the role of the top leaders, Mao Zedong and Chiang Kaishek, to carry out state violence and the strategies used by the regime to carry out the plans of the respective top leader.<br />
Based on this analysis it is argued that under totalitarian and authoritarian regimes, violence is not simply a means to maintain control. Instead, it provides an institutional support for the regime. In the case of mainland China in the 1950s, the state violence was an integral part of the CCP ideology that sought to transform society. Violence is effective because it creates omnipresent fears in society--fear of mutual-accusations between colleagues, fear of being watched by the secret police, and fear that personal opinions might lead to punishment. Such fears lead to self-censorship, first by individuals and then by the entire society. State violence establishes a prison in every individual’s inner consciousness, and this prison is the secret to the success of the dictatorship.<br />
