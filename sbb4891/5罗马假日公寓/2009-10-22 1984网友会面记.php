																											<div id="postmessage_295300" class="t_msgfont">1984网友会面记<br />
刘松萝<br />
<br />
今年8月的时候，1984的网友会了一次面。看到了张书记，看到了网名熟悉或者不熟悉的网友，他们都还很年轻。<br />
<br />
聚会的缘由，是欢迎宋石男。我和宋石男不熟，只知道他是我的好友亦忱的天敌，亦忱在书里面没少骂宋石男。提起亦忱，宋石男一摆手：他啊，何足……<br />
<br />
宋石男不愧是名士风范，侃侃而谈。谈自己，谈女人，喝酒而且谈酒。众人没有洗耳，却也是认真地听着。谈起这几天的活动，宋君连说乏味：都是一些老男人！女的不多，美女更少！今天，这里女士也不多啊。接着，他举杯：喝啊，我看大家喝的不够啊。我听了，欠起身来说：主要是段位不够，所以在酒色上面不敢过于积极。<br />
<br />
有一个插曲。有位小妹妹，17岁，要去美国留学。我对身边的朋友说：让未成年人参与政治，是不是刘胡兰重现啊。此时，有一人说，17岁不算未成年了，假如强奸……网友说：你说什么那！我知道，当然没有恶意，仅仅是文人思维发散，再喝了酒而已。<br />
<br />
有一对夫妇带着小孩子来了。孩子他妈从发型到装束，很像民国时代的人。孩子大约两岁，很可爱，不闹，心情愉快。被大人碰倒了，也只是叫了两声。孩儿妈把他放在沙发上，用他的小脚去碰小嘴。<br />
<br />
聚会结束了，那一家三口上了一辆小车，丈夫开着车走了。真是赞叹，带孩子这种国人所认为的重大事项，让他们做得如此轻松。就凭这一点，他们就是超脱之人。<br />
<br />
但愿，我们的努力能够让孩子离幸福更近，而不是更远。<br />
<br />
2009.10.22.</div>

							
							
							
							
