&nbsp; &nbsp;十几分钟后，该警到警局（身着便服），然后与该警局的警察咬耳私语了一下，后叫我跟着他走，上了该警局的2楼，该X姓警又问该警局的人要了些笔录纸，我就意识到不对了，有大事。接下来就是做笔录，该警也没问户籍的问题，直接了当的就问我平时是否喜欢上网什么的，因为已经有些心理准备了，所以就说还好，平时的话学校里，网吧会上上网。然后该警有客套的问了些其他的事，期间并没有将“艾未未”的事情告之，过了会又见来两个警察，1个比较年轻，估计是因为给我做笔录的警察年龄比较大，不熟悉互联网，需要有人陪同，另一个看肩上的星应该是该警局的高级别管理者（以下为了简单，分别称为1笔录警，2网管警，3头头警）。另外两人进来后，给我做笔录的笔录警将包内的一份文件拿出给头头警看，之后气氛就变了，他们开始以高分贝的语气和我质问我，是否知道艾未未。<br />
