（六）关于一些大家关心的问题 <br />
<br />
1. 关于循环作废的问题 <br />
<br />
这就是CPA相对比较变态的地方，ACCA是10年，CMA，CFA，中国精算师等都没有时间规定。而CPA规定是两个五年，什么是两个五年呢？CPA实行时6＋1的考试方式，6指的是会计、审计、财务成本管理、经济法、税法、公司战略与风险管理，这6门叫做专业阶段考试，重点在于考察我们的专业知识。1就是综合测试，主要考察我们的实务能力。专业阶段6门，每一科有5年的有效期，举个例子，第一年过了会计，第二年过了税法，第三年过了审计，第四年过了公司战略与风险管理，第五年不就还剩一门经济法吗？如果第五年不幸经济法没有过，第六年不仅要考经济法，而且第一年的会计也会作废，会计也要考，如果第六年经济法才过而会计没有过，则第七年不仅要考会计，第二年的税法也会作废。因此就会出现有的考生考了十几年CPA还没有考出来的杯具….然后第二个五年是指，拿到专业阶段合格证后，有5年的时间考综合测试，如果5年内综合没有考出来，之前的6门专业阶段的就全部作废。 <br />
<br />
<br />
<br />
2. 关于报名方式时间的问题 <br />
<br />
CPA现在基本是网上报名，现场确实。也就是说网上（<a href="http://124.205.65.66/index.html" target="_blank">http://124.205.65.66/index.html</a>）报好科目后到现场自己再看一看顺便把钱交了。一般报名在3~4月份。费用不太清楚，不贵，当初我5门也只有500多。 <br />
<br />
<br />
<br />
3. 关于报名资格的问题 <br />
<br />
财政部是这样规定的 <br />
<br />
符合下列条件的中国公民，可报名参加考试： <br />
　　1．具有完全民事行为能力； <br />
　　2．具有高等专科以上学校毕业学历、或者具有会计或者相关专业中级以上技术职称。 <br />
　　有下列情形之一的人员，不得报名参加注册会计师全国统一考试： <br />
　　1．因被吊销注册会计师证书，自处罚决定之日起至申请报名之日止不满5年者； <br />
　　2．以前年度参加注册会计师全国统一考试因违规而受到停考处理期限未满者。 <br />
<br />
很多同学会问大学生能不能报，几年级可以报，怎么报，明确和你讲可以的，但鉴于这个问题比较敏感于复杂，具体问题大家可以加我好友私聊。 <br />
<br />
<br />
<br />
4. 关于通过率的问题 <br />
<br />
每年的出考率一般是50%左右，也就是说报了名只有一半人去考。去考的那些人里通过率是12%左右。也就是100人报名6个人通过。大家听到这里觉得通过率很低是吧。其实不然这门考试几乎一半考试的都是上班族。他们虽然有经验，但是时间是绝对不够的。相比大学生去考，个人觉得单科通过率这么也会上30%。而且比如许多童鞋报6门，真正玩命去读的可能之后2门，另四门就是重在参与，体验一下。根据我的调查，周围的同学只要认认真真读的话，不管报了几门，至少有一门到两门过的！总之你们看到了通过率比较虚，排除上班族和重在参与的人之后，通过率应该还是比较高的！ <br />
<br />
<br />
<br />
<br />
<br />
5. 有关于CPA的职业规划的问题 <br />
<br />
我咨询了很多前辈，一般没有经验的CPA是一点都不值钱的，而CPA加上4~5年工作经验一般在大城市能拿到20万左右的年薪，CPA一些是在事务所往上做升合伙人，绝大多是都是跳槽去做企业的财务经理，然后往企业的高管发展，少数CPA会进入专职的咨询投资领域。在我学习的时候一些考友几乎都是已经工作了，工作后其实学习的时间相对就很少了，作为大学生有着充足的时间，而且如果大学毕业可以拿到CPA资格的话，你就已经赢在起跑线上了！ <br />
<br />
<br />
<br />
（七）关于事业的几点思考 <br />
<br />
各位，如果你能看到这里，我觉得已经不容易了，我想分享一下我的经历或许对你有帮助，我本人从高二开始创业，一开始做的是玩具批发，到了高三下半学期，被父母逼的参加高考，考进了华东政法大学，我非常谢谢他们，否则我可能依然像以前一样无知。 <br />
<br />
高考考完我仍然在创业，开过魔术培训班，做过网络社区，淘宝做卓越图书代购做到三钻石，进了大学我组建了200人左右的信用卡团队，说实话真的赚了一票。但与此同时我见到了千千万万的创业者，99%的失败者，1%的所谓的“成功者”依然为了生存苦苦挣扎。大学生创业潮的背后是一个一个血一样的失败。 <br />
<br />
我开始思考我的人生，其实大学生创业的成功才是真正的失败！！他们不断地投入时间、精力创办一个企业，到头来绝大多数都是在维持生存，我敬佩他们，我曾经也是他们的一员。如果你们想听故事我们可以一起吃个饭，呵呵。但是，话说回来，我不想重走他们的路。但到底怎么做呢？当我迷茫时，偶然接触到了CPA… <br />
<br />
CPA不是绝对的，你想有所成就不一定要考CPA，你可以考ACCA，CFA，司法考试，同声传译，MBA等等，为什么我一味强调考试呢，我的一位老师说过，考试的目的不单单是拿到证书，更重要的是在考试中磨练自己，收获自己。实践经验固然重要，不过我善意的提醒你，当你走上社会你一生都在实践，而下一段像大学里一样无忧无虑的没有家庭负担的学习时间，可能就是你退休之后了。 <br />
<br />
其实作为一个男生，要让奋斗成为一种习惯，30岁之前的时候你有无限的潜能和机会，一切还都有可能，30岁之后你会承担起家庭的重负。你的激情已经不如从前。有没有想过，你的一生可能会在平庸中度过？</div>

							
							
							
							
