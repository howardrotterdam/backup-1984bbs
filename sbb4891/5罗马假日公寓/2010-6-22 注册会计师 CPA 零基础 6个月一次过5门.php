<br />
（一）简单介绍一下 <br />
<br />
先介绍一下CPA吧 CPA = Certified Public Accountant，注册会计师，是指依法取得注册会计师证书并接受委托从事审计和会计咨询、会计服务业务的执业人员。具体大家可以看百度百科<a href="http://baike.baidu.com/view/7617.htm?fr=ala0_1" target="_blank">http://baike.baidu.com/view/7617.htm?fr=ala0_1</a> <br />
<br />
在我看来作为一个会计师，其实只要懂一门会计就可以了，CPA原来在我看来确切的说应该是审计师（大家不要拍我…），因为CPA本身拥有上市公司财务报告的签字权，这些是ACCA、ACA、CMA之类所没有的。 <br />
<br />
而现在的趋势来CPA的知识结构有了巨大的变化，就拿其中的经济法举例，经济法不仅有众所周知的财会法规，更是包括了，法理、民法（总论、物权、债）、商法、经济法、知识产权。作为法学专业的学生，个人认为如果加上保险法然后充实下民事诉讼法，其业务水平在民商法方面绝对不会输给律师！税法也可以为CTA打下坚实的基础。 <br />
<br />
财务成本管理除一些传统章节外，新整理的证券与金融衍生品投资、企业价值评估等等更是涉及到金融乃至投行的一些业务。 <br />
<br />
最让人费解是新制度新增的公司战略与风险管理，连经济学、营销学、人力资源甚至计算机的内容。我考完一遍感觉CPA越来越倾向于成为企业的CFO或者CEO <br />
<br />
我力图用最通俗的语言和最现实的方法来告诉我是如何复习的。当然学习方法多种多样，找到自己的方法才是王道，别人的只能做参考。 <br />
<br />
<br />
<br />
（二）零基础的心态 <br />
<br />
再次强调一下，这篇文章是写给零基础看的，那些所谓的状元（今年老制度财管还有人99分….汗）可能方法完全和我不同，如果您想花几年的时间就为考出一门高分的话，就不要看我的文章了。 <br />
<br />
有很多人问我CPA难不难，我研究所有考试，从纯技术的角度来看，除了精算师之外几乎没有超过CPA的了，这点可能大家有别的看法，欢迎探讨~ <br />
<br />
大家都是零基础，个人认为只要花3到4天就可以掌握基础的会计的内容，我是这样分析的假设会计基础课会计系一周一次每次3课时，一学期的话18周左右，一共就是3*18*45/60=40.5个小时，去掉老师扯淡的时间，20个小时，2天也应该够了。 <br />
<br />
最重要的心态就是向前看，你往前看历年都有一年过5门的，最牛的2个月零基础拿到全科合格….汗，当然新制度考试变成了6门，今年50万（不可靠资料）考生，起码有几十个过6门的。你如果往后看的话，由于循环作废，有的考生考了11年还没有拿到证。关键就看你和谁比了！ <br />
<br />
CPA对你意味着什么，要不要必须过这个考试，是一次过还是只不过做个试验？首先思考这个问题。如果是肯定的，请继续。 <br />
<br />
然后思考你愿意付出多大的精力去过这几门考试，能否做到每天8~10个小时的学习时间，能否做到心无旁骛地看书复习，能否做到六个月不玩任何电脑游戏，能否做到半年不见自己的另一半？如果愿意付出这些代价（当然实际上不会这么痛苦），请继续。 <br />
<br />
简而言之，决心才是最最重要的，它才是你从头到尾必须要有的东西，而它也必定会帮助你克服CPA复习过程中的所有困难！记住，是所有困难！ <br />
<br />
<br />
<br />
（三）书籍与辅导班 <br />
<br />
CPA的各科教材难度我认为是相当的高，教材的体系尤其是会计和公司战略与风险管理，个人认为是相当的混乱，例题也是很有限的，不用书籍理解起来还是很困难的。这里主要推荐“中华会计网校”的“梦想成真”和“东奥会计在线”的“轻松过关” <br />
<br />
这里推荐两个网校和两个面授班 <br />
