（五）关于复习的方法 <br />
<br />
首先要强调一点，大家千万不要畏惧CPA，只要我们拿时间去砸，总有一天会考出来的。有一句话送给大家：“过程做的好，结果自然好！” <br />
<br />
其实是要为自己规划一个进度，做进度的时候做到7分就好，比如你每天有10个小时的学习时间，你安排到7个小时就可以了。给自己留有余地，同时保证计划的可行性。千万不要一时心血来潮，给自己每天定十几个小时的学习时间，一方面是完成不了，就算勉强完成效率也是极差的。 <br />
<br />
第一轮的复习：三月初到六月底。第一轮是时间是最长的，因为不熟悉教材，这一遍的学习重在理解。很多细节的地方先不要记忆，比如会计的收入那一章有关工程的会计处理，先理解。如果不懂可以多听几个老师的课，一知半解就可以了（大概掌握60%）。在这一轮的学习中我们要集中火力一门一门来，如果东看看西看看，可能就会事倍功半，得不偿失。这一轮是最最最最痛苦的，坚持过去你就成功一半了！ <br />
<br />
第二轮的复习：七月初到七月底。再次翻开你曾经看过的第一门，你会发现几乎全部忘记光了，别担心，这是正常现象。再炒一遍冷饭你马上就会回忆起来，同时这次的学习会给你留下更深刻的印象。冷饭抄完之后，大家可以去鲤鱼网下载一下中华会计网校的专题讲解班，结合不同的章节，重点难点逐个击破，能学会几个就学会几个。整体掌握度70%。这轮的复习也是一门一门的，同时要配合少量的做题。不管是“轻松过关”还是“梦想成真”，挑一本每个章节题目都过一遍。计算题不要过分纠缠，思路知道就马上跳过！同时听网校的习题班。 <br />
<br />
第三轮的复习：八月初到八月底。有人说考试前很关键，其实不然，第三轮才是至关重要的一轮，考前已经八九不离十了，而第三轮还有余地来弥补，所以成败在此一举！这一轮相对也是比较痛苦的，虽然知识基本掌握，但是之前的学习已经消耗很多的体力，感觉也比较累。这一轮咱们的特殊点是在于所有科目要同时进行，如果是一门一门来的话，到冲刺的时候可能第一门复习的又有些遗忘，大家坚持住。这一轮没什么花头，就两字――做题。CPA不仅要搞懂，还要答对；不仅要答对还要在考试时间内答对，所以题目的熟练度是相当重要的。值得一提的是，做题不是乱作，争取把题目做精，做完之后问问自己，出题者的意图是什么，如果我是出题者我会怎么出？通过做题加深理解，争取掌握（80%~85%） <br />
<br />
第四轮的复习：九月上旬。最后的冲刺，这一段时间还可以冲一冲。10天的时间把中华会计网校或者东奥会计在线的模拟题做一遍。这一轮的目的一方面是进入考试的状态，更关键是能够让自己和更强的对手先打一架。网校的模拟题那是相当难，坦白和大家交代我会计、财管模拟卷没有一次是及格的… 有时甚至会出不规则卷，就是难度和题量均按照正常考试的1.5倍来出，真的会做疯掉的。但是经过这一轮之后，你面对CPA考试就有一览众山小的感受了。 <br />
<br />
第五轮的复习：九月中旬至考试，这一轮已经没有必要再多做题了，要做的是把做过的，尤其是错的题目多看两遍，同时听听网校的冲刺班。每天晚上早点睡。如果你下午有考试，千万别睡午觉，不然会打乱生物钟的。 <br />
<br />
临场注意事项：身份证、准考证、铅笔、水笔、计算器、餐巾纸都要带好。出门看好天气预报，吃东西不要太油腻。考前不要讨论题目，考完不要对答案…… 总之参考高考…… <br />
