<br />
简单点评一下，我个人感觉，网校和面授班各有好处，网校我很推荐，首先网校的收费比较低，还可以在淘宝上6折充值，性价比相当高。其次师资力量强，网校由于是远程教育边际成本几乎为零，所以可以花重金聘请名师，而面授班则做不到。然后网校的话可以随时听，不限次数（每门课我都听了3遍）。最重要的就是网校一般都有答疑，24小时无论多复杂的问题都会提供详细的解答。面授班的话主要就是可以当面交流，找找感觉，认识一点朋友。 <br />
