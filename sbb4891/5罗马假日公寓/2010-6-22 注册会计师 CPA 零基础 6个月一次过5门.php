（四）各科介绍 <br />
<br />
1. 会计 <br />
<br />
《会计》是CPA考试最重要的一门课程，难度也是属于第一梯队的。将来在工作中可能也是最实用的一门，作为审计的基础，听前辈说审计40%是在考会计。所以请大家务必重视这门学科，不仅要会做题，做好能够深入的理解！比如为什么可供出售金融资产的变动要进资本公积――因为要防止管理层操纵利润。会计这门课是属于一个萝卜一个坑，分录一笔一笔做清楚的话就没有什么问题了。郭建华说2008年最有一道题也是把分录都默写出来然后代数字！ <br />
<br />
再强调几个问题，摊余成本一定搞清楚，书上很多地方都出现。细节要注意，掌握要全面，很多同学考完之后觉得卷子出的很“偏”其实就是没有掌握到位。或有事项、每股收益等等千万别忽视！ <br />
<br />
<br />
<br />
2. 财务成本管理 <br />
<br />
《财务成本管理》是今年让广大考生啼笑皆非的一门课，广西的45%的出考率中通过的人数也只有6%左右，也就是说100人报名只有3个人考出来，而且感觉那3个人也是明显放水的，本人自以为财管是最拿手的，考完试直接崩溃，事后估分48分，最后竟然很神奇的拿到了62分！如果说放水也只有3%的人能通过的话，这门课也太变态了。大家要引起足够的重视，我个人感觉吧为什么明明难度不是最高的科目通过率会那么低，可能是大家光学习不做题，到了考场上看看都见过，做做都错掉…濉 <br />
<br />
财管这门课如果大家要往管理会计方向发展或者要做咨询、投资等业务的话一定要学好，挺有帮助的。财管在学习时要注重理解，同时多加练习 <br />
<br />
<br />
<br />
<br />
<br />
3. 税法 <br />
<br />
《税法》个人认为是非常实用的一门学科，除了众所周知的两种盈利方式，实业和资本运作外，税务是作为企业第三种盈利的渠道。其中偷税、避税、税收筹划大家仔细比较和研究的话，真的奇妙无穷。之后我可能要写一篇有关税收筹划的文章，还望大家多多指教~~ <br />
<br />
税法让人头大就是里面的各种减免税的规定，而且每年都会有更新，这门课力求一次性通过，今天选择题的计算类型的题目道道都有陷阱。我有位同学对过答案后面大题几乎全对，成绩出来也只有65分，传说都是栽在选择题上。平时怎么做，你做完一遍多想想别的3个选项都是在哪里给你设套了，这种小伎俩平时看多了，考试就不容易跌进去。再谈谈大题目，CPA税法特点就是没有步骤分，比如一道增值税的题目，第一步错了，如果后面的计算都是以第一步为基础展开的话，那你就惨了。。。。有位考友形象的是描述就像皇帝株连一样，错杀一片。所以税法一定一定一定要仔细！！！ <br />
<br />
<br />
<br />
<br />
<br />
4. 经济法 <br />
<br />
终于到了我的老本行了，说实话一开始拿到教材我觉得没什么可以看的，毕竟是本专业嘛，后来看看才发觉还是很有难度的。经济法可以说是独立于整个CPA考试的，几乎和其他学科没有什么牵连，但是学习的思路就完全不同了，比较像司法考试，呵呵 <br />
<br />
经济法，就像其作者自己所说，不是传统意义上的经济法，结合了法理学、民法、商法、经济法、知识产权法、还有一点点民事诉讼法。内容还是有一定得深度，虽然没有司法考试考题活 ，但是却比司法考试多出很多数字。大家学习时要有耐心，不要人云亦云说这门课很简单考前一个月看看，作为法学专业的学生，我可以很负责的说，如果考前一个月才看，除非你是神或者只考这一门，不然必挂的！大家要引起重视啊！！ <br />
<br />
这门课司法考试的张海峡讲得特别好，可惜张海峡不教CPA经济法，接下来推荐的就是郭守杰老师，郭帅~~ <br />
