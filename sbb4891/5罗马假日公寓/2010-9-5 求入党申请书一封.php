																											<div id="postmessage_815746" class="t_msgfont">中国国民党海外支部、总支部评议委员会设置办法<br />
<br />
中华民国95年10月23日第46次中央工作会议通过<br />
<br />
第一条 本办法依据《海外党务组织规程》第七条之规定订定之。<br />
<br />
第二条 海外支部、总支部得置评议委员会，其成员由支部、总支部所在地区之中央评议委员为当然委员。并可由支部、总支部及所属之分部推荐具声望之同志，经支部、总支部委员会同意后，报请中央颁发证书。<br />
<br />
第三条 评议委员会对支部、总支部及所属分部之党务有顾问与监督之权；发生考纪及争议时有仲裁之权。<br />
<br />
第四条 评议委员人数由支部、总支部委员会决定，任期与所属党部之委员相同。<br />
<br />
第五条 评议委员会采共同决议制，每次开会时推选主席主持会议，对议案之表决采多数决。<br />
<br />
第六条 本办法经中央工作会议通过后施行，修订时亦同。<br />
<br />
 <br />
<br />
中国国民党海外党员缴纳党费实施要点<br />
<br />
中华民国95年12月18日第54次中央工作会议通过<br />
<br />
一、&nbsp; &nbsp; 本要点依据《中国国民党党员缴纳党费办法》有关规定订定之。<br />
<br />
二、&nbsp; &nbsp; 本党党费，分为党员党费、党员捐献两种；党员缴纳党费为党员应尽之基本义务，海外党员依本要点之规定办理。<br />
<br />
三、&nbsp; &nbsp; 海外党员依下列原则缴纳党费：<br />
<br />
（一）&nbsp; &nbsp;&nbsp;&nbsp;海外党员在党籍所属分部缴纳党费。<br />
<br />
（二）&nbsp; &nbsp;&nbsp;&nbsp;每人每年缴纳金额为美金10元，月缴美金1元。<br />
<br />
（三）&nbsp; &nbsp;&nbsp;&nbsp;党员得视意愿主动提高认缴党费标准。<br />
<br />
（四）&nbsp; &nbsp;&nbsp;&nbsp;一次缴纳党费美金300元以上者，终身毋需再行缴纳党费。<br />
<br />
（五）&nbsp; &nbsp;&nbsp;&nbsp;海外分部得比照当地币值定价。<br />
<br />
四、&nbsp; &nbsp; 海外特殊地区无法依上列原则缴纳时，得另订定标准，经向中央报备核定后实施。<br />
<br />
五、&nbsp; &nbsp; 海外分部于收取党员缴纳党费时，应开掣收据，并在党籍资料中注记。<br />
<br />
六、&nbsp; &nbsp; 海外分部党费所得全数留在分部做为发展党务、服务党员、举办活动之用。每年需向中央组织发展委员会海外部列报党费收支情形。<br />
<br />
七、&nbsp; &nbsp; 分部委员会之财务负责人需将党费收支情形，于每次委员会及党员大会时提出报告。<br />
<br />
八、&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;党员依规定应缴纳党费而未缴交者，暂停一切党权。党员补缴党费后四个月，始恢复应有党权。超过两年未缴纳党费者，视为自动退党。<br />
<br />
九、&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;本要点经中央工作会议核备后施行。<br />
<br />
 <br />
<br />
海外党员筹设分部实施要点<br />
<br />
一、&nbsp; &nbsp; 党员欲成立新分部应先组筹备委员会向中央组织发展委员会海外部报备，经核准后始得进行筹组工作。筹备期间以筹备会名义向中央行文。<br />
<br />
二、&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;新分部成立之党员人数，依所在侨社规模大小而有不同的下限，侨社规模较大之地区，以至少50-60人原则，侨社规模较小之地区则为30人。<br />
<br />
三、&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;新分部应向中央函报党员名册，分为三类列报：<br />
<br />
1. 新入党党员，需填「入党申请书」，完成入党手续；<br />
<br />
2. 回复党籍（归队） 者，需填「回复党籍申请书」；<br />
<br />
3. 党籍隶属现有其他分部的党员，需亲填「党籍移转申请书」，且本类党员不得超过新分部党员之半数。（相关表件如附件）所报名册经查核无误且人数达下限后，始得正式成立。<br />
<br />
四、&nbsp; &nbsp; 现有党员人数较多（超过200人）之分部，得分别成立或改组为数个分部，因涉及现有委员之任期与权益，需经现有委员会议通过后，将新分部成立（改组）之计画，报中央核定后进行。<br />
<br />
五、&nbsp; &nbsp; 新分部委员会委员应召开党员大会直接选举，委员人数以每10位党员产生一位、选举时间以党庆（11月24日）前后为之为原则，委员任期三年，自次年一月一日起算。<br />
<br />
六、&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;委员会产生后筹备会自动解散，依党务法规运作，并填报「海外党部资料表」、自订党部名称，经核定后颁授印信及委员当选证书。</div>

							
							
							
							
