<br />
个人建议吧：如果希望自己的“独立思考”能有理论基础，建议学习《宪法学》《法理学》《刑事诉讼法学》《民法学》《行政法学》。学习方式自选，教材、教程、录音或视频以公认的名师为准，学习的内容以【应然】为准。<br />
<br />
理由：<br />
《宪法学》可以让人明确 公民的权利、国家应当如何存在及其意义、公民与国家的关系，反洗脑第一课。<br />
《法理学》可以让人明确 什么是法律，法律与其他社会因素的关系，以及我们对法律应该做什么样的价值判断。<br />
《刑事诉讼法学》最核心的内容就是 为什么要保障人权以及如何保障人权。<br />
《民法学》最核心的内容就是 人与人之间为什么要平等，以及如何保持平等。<br />
《行政法学》可以让你明确 一个正常的政府应该是什么样的，公民和政府应该是什么样的关系。<br />
<br />
