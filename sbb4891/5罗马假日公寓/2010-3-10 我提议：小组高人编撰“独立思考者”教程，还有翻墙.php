													<h2>我提议：小组高人编撰“独立思考者”教程，还有翻墙</h2>
						
						
																											<div id="postmessage_510507" class="t_msgfont">虽然凡是能够进入小组者，必有一定的独立思考的能力，但是思考的方式、深度、广度必定参差不齐。我坦言，我个人还不具备完全独立思考的能力，因此我提议，小组高人编撰“独立思考者”教程。<br />
1、编撰教程可以采取引导读书的方式，就像导师带学生一样、指点读哪些书或者看哪些电影即可。<br />
2、教程可分为启蒙篇、入门篇、提升篇和交流篇等等。<br />
3、列出书目或者资料名录或者电影等名录后，由小组成员自己去找，如果找不到、再寻求帮助。<br />
4、还有很重要的一个部分，就是翻墙，我不是学计算机的，而且我所学过的计算机知识并不能帮助我顺利地翻墙，我至今仍使用一些简单的软件翻墙，对于什么SSH，VPN简直一窍不通，我反对翻墙仅仅作为计算机精通者的权利，因此提出这个要求。<br />
5、暂时没有想到更多，如果想到我会补上，大家也帮忙补充。<br />
