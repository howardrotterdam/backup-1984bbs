																											<div id="postmessage_333528" class="t_msgfont">寂寞的人有福了<br />
<br />
<br />
寂寞的人有福了，这是我最近的签名档。我的签名档总是有出处和典故的，这次也不例外。当然，以上说法是我的自我标榜，其实，客观的说法是我这签名档是抄袭来的。它是一首歌的歌名，《寂寞的人有福了》。<br />
<br />
之所以选择了这个作为签名，是因为我很快就有福了――我买了一辆红色两厢福克斯1.8MT。我现在在等她，等待中的人总是寂寞的。于是，寂寞的人有福了。<br />
<br />
以上，只是这个签名最简单最没内涵的成因。<br />
<br />
可是昨天晚上群里面说到了黄耀明。我多么的喜欢他，以至于用“妖娆”、“风骚”来形容这枚讲粤语的男子。于是，我得到了一个疑问：你用这个来形容一个男人？可是又有什么办法，我还喜欢过一个叫张国荣的男子，到最后，他不也只能用“妖娆”、“风骚”来形容么？这是没办法的事情。<br />
<br />
让我想想，我为啥喜欢明哥。<br />
<br />
首先，他声音好听。这在达明一派时代就经过了时间和大众的考验。那个时候他就是主唱，音乐多是刘以达谱曲。他声音很干净，高音的时候像丝绸一样柔滑。他声音不惧检验，现场版的《暗涌》几乎可以跟CD没有什么分别。<br />
<br />
其次，他漂亮。我没有说他英俊，虽然其实他挺英俊的。他像一朵盛开在寂静森林最幽暗处的曼陀罗，千姿百态，熠熠生辉又有毒害。是的，他是曼陀罗，中毒之后口渴舌燥、声音嘶哑、面部潮红，甚至瞳孔散大、出现幻觉――这，多么像他的演唱会。<br />
<br />
第三，他懂。他有最敏感的心和最敏感的捕捉感知的能力。他的歌涉及的领域宽广，很多歌曲难以归类。《禁色》应该是讲同性爱的吧，《这么远，这么近》是讲同性爱的还是讲冥冥中很多人之间的缘分之差那么一点的遗憾呢？《Mon chocolate》是讲因爱为之融化的美妙还是越爱越迷失的迷惘？《同一个世界》是讲人和人距离究竟是远呢还是近？《阿姆斯特丹》是说一个人旅行能找到新的自我呢还是讲其实只要对自己诚实，人永远都无法解脱？太多了，无法一一尽述。献上《寂寞的人有福了》的歌词，希望每个人不管怎么寂寞，都能像歌词中描述的那样，把犯过的桃花像涂鸦一样洗掉，这样，是不是就更加完美了？哦，不要打搅这感觉，喜欢明哥的人不会失恋，寂寞有害的是哥哥，我们寂寞的人有福了。<br />
