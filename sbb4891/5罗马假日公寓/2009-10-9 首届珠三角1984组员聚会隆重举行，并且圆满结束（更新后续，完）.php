自然，桌上的话题也不同。严肃派话题是民主自由，在北风组长的带领下，从苏格拉底雅典时期的朴素民主谈起。而低俗派则是感官自由，在麦组长的指挥下从《罗马帝国艳情史》开始。但后来众多低俗派组员回忆起这一段往事时，无不扼腕叹息，切齿长嚎。因为全场仅有的三个漂亮女组员都是热爱民主自由，无视感官自由的。众组员看着三位女组员无比坚毅地飘到隔壁桌，第一次真切地感受到民主自由的重要性。<br />
<br />
著名的公务员组员（dacenke？）满腔忧郁，问同是无比失望的低俗派组员：你们知道，用什么喻体比如三位女组员最合适吗？<br />
<br />
浮灰抹掉口水，天真地对曰：花啊，而且是白玉兰。<br />
<br />
公务员组员说：错！是鸭子。但飞走了，飞到隔壁去了。<br />
<br />
然后，一片叹息，落英缤纷。<br />
<br />
<br />
<br />
低俗派满腔不忿，严肃派则继续高谈阔论，激动处，口沫横飞，指点江山：彼可取而代之。<br />
低俗派决定化郁闷为抗争，就像《明野史》里记载到的华安和宁王的参谋“对穿肠”对对联一样，每次听到严肃派讨论到严肃沉重的话题后，立即搬出无比低俗的话题与之抗衡。<br />
<br />
<br />
<br />
“他们说到美国的宪法史了。”<br />
<br />
“那我们研究研究《肉蒲团》吧。”<br />
<br />
<br />
<br />
“他们说到黑人争取种族平等的权利运动了。”<br />
<br />
“我们分析一下日本片和日本女党员如著名的松岛枫党员是怎样走进每个人的心灵的。”<br />
<br />
<br />
<br />
“他们终于说到64了。”<br />
<br />
“我们缅怀饭岛爱党员逝世引发的操蛋的青春感伤吧。”<br />
<br />
<br />
<br />
……………………<br />
<br />
<br />
<br />
结果当然是，华安赢了，而低俗派的“对穿肠”们则每人喷了至少一升血。后来有人指出，其间有人喷出的血液不是红色的，而是黄色的，还带酒精味。毫无疑问，此人就是著名的五毛1984sb。<br />
<br />
<br />
<br />
据说，1984sb从深圳赶过来的，但估计他这晚比较郁闷。<br />
<br />
他老人家一拿起筷子就嚎叫服务员：两打酒！<br />
<br />
“什么酒？”<br />
<br />
他转向组员，问：你们一般喝什么酒？<br />
<br />
众组员对曰：可口可乐酒。<br />
<br />
1984sb显然当场被震精了，满桌的男组员居然都不喝酒，于是再次嚎叫服务员：那两打酒先放进冰箱里，来三支可乐三瓶啤酒得了。<br />
<br />
sb不甘独酌，终于硬是拉上还在读书的小朋友Geona一起喝（有围观过群p照片的同学指出，Geona酷似李小龙，话说，我曾记得Geona小同学曾在我写的一篇好像叫“韩寒为什么成为不了好作家”的文章里回复我，让我“速领五毛”……哼哼……）。喝到情深处，1984sb吹起自己的记忆能力来，号称在超过一千个网站注册过用户，用户名全部不同，但他都能记住。后来有人揭穿他，说他都是以网站的域名为前缀，“sb”为后缀注册用户名的，比如1984sb。他又嚎叫：绝对不是这样，否则我是sb…………算了，都临近醉与睡的边缘了，原谅他吧。<br />
<br />
<br />
<br />
我们低俗派和严肃派的对抗一直到9点多吧，大概。低俗组员吐完血后，纷纷到严肃派那里表示“告辞”，要先走了。严肃派就寒暄一番后表示一路顺风，叮嘱低俗派组员下次记得学好民主自由再来聚会，否则还是无法争取到民主自由MM们的青睐。剩下的低俗派见组织散了，也就归顺到严肃派那一桌。准备先向前辈们学习一点民主自由，好用在下次聚会上争取MM的眼神。谁知道反对派一走，严肃派们就不谈民主自由了，讲起了坊间八卦故事。北风组长吹嘘自己当街拿凳子砸过一头饿狼，讲完砸狼后，还论证砸狼的合法性和合理性；然后还说自己调戏过机场安检的故事；如此如此，这般这般……一直到大概10点吧。<br />
<br />
<br />
<br />
然后大家就如周云蓬唱到的那样：天黑了，灯亮了，回家吧……………………<br />
<br />
（完）<br />
