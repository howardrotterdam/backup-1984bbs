标&nbsp;&nbsp;题: 最近联系工商银行北京总行的海归职位的过程和结果<br />
发信站: BBS 未名空间站 (Mon Dec 28 05:44:31 2009, 美东)<br />
<br />
猎头主动联系我。<br />
首先被要求把简历改写成中文。英文简历他们不接收。并被要求写上很多隐私信息，包括生日，籍贯，婚否，是否党员之类。晕。估计工行领导不认识英文。<br />
<br />
然后面试过程和其他公司类似，但是感觉很别扭：<br />
<br />
1. 国内人英文其实很差，但是有点井底之蛙很自以为英文流利。面试过程中反复提到vice president, 但是总是发音成voice president。我说到compensation的时候，对方卡壳了，听不懂这个词。只好改成中文对话了。<br />
2. 视频对话，我打开我这边摄像头，对方借口摄像头坏了，不肯打开给我看。感觉很狡猾。<br />
3. 我习惯直接称呼对方的姓名，但是他希望我改口叫X总，X长这样的称呼。<br />
4. 对方一个，一上来就以为我人在加拿大，我说了几遍他才搞清楚我在美国。<br />
5. 还有一个，认定我是在美国失业很久了，混不下去。我告诉他我现在的工作和收入都很好。对方明显很失望。<br />
<br />
歇会儿，再写。。。<br />
<br />
哈哈，看了大家的回帖，一些人的心态确实比较有意思。<br />
<br />
对于申请国内职位的海外华人，无论国内的人还是国外的人，大家一致认为是在国外混不下去了才联系回国的。就算你不是这样，也跳进黄河洗不清。国内企业给你面试，一上来就不惜一切手段打压你，直到他们相信你在美国走投无路，每天吃救济，睡大街，<br />
然后他们才表现出少许欣慰的叹息。哈哈。在这样的氛围下，你没可能拿到好offer.<br />
<br />
至于说到居高临下的姿态问题，要说犯错也是对方在先。没搞清楚状况就对这边的人居高临下，有没有搞错啊。是你要来挖人的，我可没求着你。对于这样一个没有基本职业素养，开的工资又低，北京消费又奇高的国家财政补贴的私营企业，我确实很难仰视。<br />
<br />
联系我的那个猎头也是chinaHR, 谁愿意把我的帖子转发给这个猎头，我完全支持啊。一定要让这帮人醒醒了。大家联系海归职位，不要一副奴才乞讨的模样，要坚守底线，不要搞坏了行情，对大家都没好处。<br />
<br />
最后拐弯抹角旁敲侧击终于把工资这个问题摆上台面来说了。对方说，这个职位的正式工资要经过试用和评审，位子保证给你留着，你先回来，工商银行就候选人薪酬福利正在做积极的调研和决策过程中。哈，当我是傻子啊？海归被忽悠回国，落地就翻脸的事情还少吗？我坚决不干。对方于是说，你的合理的期望工资是多少啊？<br />
<br />
我对比了一下两地的物价，我工作的地区，一套condo 30万美元，一辆奔驰车6万多美元。开车上班30分钟。北京这样的房子，大概300~400万人民币，合50万美元。相同档次的奔驰车，大概在 90~100万人民币，相当于14万美元。北京的电器服装汽油食品教育医疗都比美国贵。我说了我现在的工资，要在北京维持我现在的生活水平，您看着给个合理的数？<br />
<br />
对方楞了半天，居然给我讲起了奉献精神。我靠，已经有10年没听到这种调调了。他说，总体国内薪酬水平就处于低位，正是这样的广大员工的无私奉献，成就了公司的快速发展。。。我要奉献也不会奉献给一个靠国家财政补贴的私营企业。<br />
<br />
我告诉他，不是你出不出的起这个价钱的问题，或者反咬一口说我值不值这个价钱的问题，而是以什么价钱我才愿意放弃我现在的工作和生活。<br />
<br />
海归，不是目的，而是拿到满意的offer之后的一个迁移过程。大家不要本末倒置。<br />
<br />
最后的结果是，工行试图说服我放弃现在拥有的一切，接受较为拮据的生活水平，但是没有充足的理由。我没有同意。跳槽不能往低了跳嘛。<br />
<br />
<br />
