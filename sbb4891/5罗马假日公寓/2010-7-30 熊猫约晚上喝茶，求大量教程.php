													<h2>面对国安、国宝、警察：你的权利</h2>
						
						
																											<div id="postmessage_765444" class="t_msgfont">你在被传唤时，有哪些权利<br />
<br />
&nbsp; &nbsp; 1、传唤时，公安机关必须出示经公安机关办案部门负责人批准的传唤证。<br />
&nbsp; &nbsp; 如果是对在现场发现的违反治安管理行为人，人民警察经出示工作证件，可以口头传唤，但应当在询问笔录中注明。<br />
<br />
&nbsp; &nbsp; 2、公安机关应当将传唤的原因和依据告知于你。<br />
<br />
&nbsp; &nbsp; 3、公安机关应当将传唤原因和处所通过电话、手机短信、传真等方式通知你的家属，但你可以拒不提供家属联系方式。<br />
&nbsp; &nbsp; （传唤的处所包括了你的住处、单位，或你所在市、县内的地点）<br />
<br />
&nbsp; &nbsp; 4、公安机关询问查证的时间不得超过八小时；如案情复杂，违法行为依法可能适用行政拘留处罚的，询问查证的时间不得超过二十四小时。<br />
<br />
你在被询问时，有哪些权利<br />
<br />
&nbsp; &nbsp; 1、公安机关必须依照法定程序收集证据，严禁刑讯逼供和以威胁、引诱、欺骗等非法手段收集证据。以非法手段取得的证据不能作为定案的根据。<br />
<br />
&nbsp; &nbsp; 2、公安机关在调查取证时，人民警察不得少于两人，并应当向你表明执法身份的，出示相关证件。如果不表明身份，你可以拒绝配合。<br />
<br />
&nbsp; &nbsp; 3、如果人民警察是本案当事人或者当事人的近亲属，或者警察本人或者其近亲属与本案有利害关系的，或者与本案当事人有其他关系，可能影响案件公正处理的，你有权要求他们回避：<br />
<br />
&nbsp; &nbsp; 4、对涉及的个人隐私，公安机关及其人民警察在办理行政案件时，应当保密。<br />
<br />
&nbsp; &nbsp; 5、询问时，你对询问与本案无关的问题有拒绝回答的权利。<br />
<br />
&nbsp; &nbsp; 6、你有权陈述与申辩，公安应当对其提出的事实、理由和证据进行复核，不得因申辩而被加重处罚。<br />
<br />
&nbsp; &nbsp; 7、询问笔录应当核对，记录有误或者遗漏的，你可以更正或者补充。对没有阅读能力的，公安人员应当向其宣读。<br />
&nbsp; &nbsp; 记载有遗漏或者差错的，你可以提出补充或者更正。你确认笔录无误后，应当签名或者盖章，询问的人民警察也应当在笔录上签名。<br />
<br />
&nbsp; &nbsp; 8、你可以自行提供书面材料的。<br />
<br />
如果你被行政处罚，你有哪些权利<br />
<br />
&nbsp; &nbsp; 1、公安机关在作出行政处罚决定前，你对拟作出行政处罚决定的事实、理由及依据，依法享有陈述权和申辩权。公安机关不得因你的申辩而加重处罚。<br />
<br />
&nbsp; &nbsp; 2、“对个人处以二千元以上（包括二千元）罚款”的行政处罚决定之前，你有要求举行听证的权利。公安机关不得因你提出听证要求而加重处罚。除涉及国家秘密、商业秘密、个人隐私的行政案件外，听证公开举行。<br />
<br />
&nbsp; &nbsp; 3、对你的同一个违法行为，不得给予两次以上罚款的行政处罚。<br />
<br />
&nbsp; &nbsp; 4、违反治安管理行为在六个月内没有被公安机关发现，其他违法行为在二年内没有被公安机关发现的，不再给予行政处罚。被侵害人在违法行为追究时效内向公安机关控告，公安机关应当受理而不受理的，不受此追究时效的限制。<br />
<br />
&nbsp; &nbsp; 5、对于违反治安管理行为事实清楚，证据确凿，处警告或者二百元以下罚款的，可以当场作出处罚决定。公安机关及其办案人民警察当场收缴罚款的，应当出具省级或者国家财政部门统一制发的罚款收据。对不出具省级或者国家财政部门统一制发的罚款收据的，你有权拒绝缴纳罚款。<br />
&nbsp; &nbsp; 你确有经济困难，可以申请暂缓或者分期缴纳罚款，但要经作出行政处罚决定的公安机关批准。<br />
<br />
&nbsp; &nbsp; 6、卖淫、嫖娼、引诱、容留、介绍卖淫，投客招嫖和赌博案件，不适用当场处罚.<br />
<br />
你对处罚决定不服，可以获得怎样的救济<br />
<br />
&nbsp; &nbsp; 1、你不服行政拘留处罚决定，可以自知道处罚决定之日起六十日内向上一级公安机关提出行政复议申请；若被行政拘留的，可以向作出行政拘留决定的公安机关提出暂缓执行行政拘留的申请。对复议决定不服的，可在收到复议决定书之日起十五日内向人民法院提起诉讼。<br />
<br />
&nbsp; &nbsp; 2、你也可以不申请复议，直接在应当在知道处罚决定之日起三个月内提起行政诉讼的。<br />
