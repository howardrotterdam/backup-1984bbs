2000年以来，吉安城建和中国其他中小城市一样变化极大，虽然这个变化的背后是对文化传承的割裂，是无数官商勾结和腐败的温床。作为当下的我们，尤其是远离家乡的我们，是无力评判这些建设对本地的进步意义――这些照片显示出来2000年的吉安市――江西中部的中心城市仍旧是一个极其破落的城市，只有3条主要的街道，无红绿灯，无公交车。<br />
