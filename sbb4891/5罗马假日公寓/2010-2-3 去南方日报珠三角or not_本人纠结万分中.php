													<h2>去南方日报珠三角or not?本人纠结万分中</h2>
						
						
																											<div id="postmessage_464035" class="t_msgfont">无论去或者不去，都是一个令我纠结万份的抉择。<br />
收到南方报业的offer，可惜不是自己的志愿 21世纪经济报道，而且，最晚2月4号一定要签，时间非常急迫，难以仔细思量。<br />
而是南方日报珠三角新闻记者。<br />
想去，又不想去。<br />
想去，是因为所谓的新闻理想，南方或许是实践新闻理想最好的地方，起码，在我的想象中是这样。或因为南方周末，或因为南方都市报，都是敢于发言勇于担当的媒体。<br />
对于一个新闻系学生而言，南方二字绝对不缺吸引力。<br />
甚至，想象中如果做得够好，往集团内部其它报纸流动也不是不可能。<br />
<br />
当然，作为一个上海人，去南方还是有着诸多困难。<br />
背离家乡，从经济角度讲，难免会增加房租和衣食开销，相对而言实际能够结余的钱可能不如在上海（在上海也有，只是在家住，所以可以相对较少）<br />
由于派驻珠三角，东莞等地安全状况令人担心。（虽然我并不是特别在意这一点，但是如果去那里工作，父母记挂在所难免）<br />
未来发展不明朗，问了在南方的学长，说起珠三角记者，他并不是特别看好，主要就是跑跑社会新闻，做得好可能半年后才有个像样的条线。<br />
说实话，父母在这时候体现的“大上海”主义令我十分不满，他们一味认为南方很乱、物价和香港接轨，而上海生活好。但我并不认为上海的生活有多好，起码高企的出行成本以及令人咋舌的房价以及对我个人而言带来巨大不便的，所谓世界文明的盛会“世博会”就足够构成我不愿再上海工作的理由。<br />
<br />
<br />
而手头现有的，是个第一财经日报的口头offer，虽然这几个礼拜基本上泡在单位，但是据说要到五月份才能签三方。<br />
做得内容是一财日报的财商周刊，说白了就是理财的内容，写写股票、基金。<br />
说实话，刚开始面试的时候还要，因为毕竟带我们的老大周迪伦还是不错的……提出的定位中闪耀着理想。<br />
但是在真正做了之后，越来越觉得厌倦。没有机会亲历任何新闻现场，所有的内容都仅仅围绕投资，实在枯燥无味。<br />
而且，需要持续实习，对于在大四还考虑好好上课、好好写论文的我而言，实在如同鸡肋，食之无味，弃之可惜。<br />
上海媒体一贯在新闻尺度上比较保守的风格，而作为文广和别人合办的报纸更是如此，虽然秦朔的总编手记写得很有远见，但是就像网上的说法一样，上海的媒体，更多的人考虑的是现世安稳，毕竟要买房买车，都是不小的负担。此外，部分记者素质须努力，也是明摆着的事实。<br />
<br />
<br />
如果签了南方，基本上毕业前可以安稳学习，没有实习的困扰，虽然入职之后到底会怎样并不明朗。此外，签了南方很可能就专心读书，放弃剩下的其它媒体了，下半学期可以预见的，起码还有文汇报（虽然前几届玩了很多学长学姐，鄙视归鄙视(#F′)凸但是终究要试试），还有只招2人，很可能后门已定的解放日报、形式并不明朗的新闻报（连晨报还是晚报都不知道，大抵晚报可能性更多）<br />
如果下半年一无所获，或许也就认命在一财呆着了，虽然肯定心有不甘，但是……又能怎样呢？所能期许的，或许就是炒股赚钱，争取早日出国读书了。<br />
<br />
站在这样一个十字路口，望着两条茫茫看不到头的路，真的不知该如何选择。无论去或者不去，都是一个令人纠结万分的选择。<br />
<br />
如果有南方的朋友愿意现身说法，我先致以谢意！<br />
如果有愿意指点迷津的，更致以万分感谢！</div>

							
							
							
							
