																											<div id="postmessage_663783" class="t_msgfont">新浪科技时间：2010-04-21 09:36:50作者：晓彬 <br />
本文关键词：苹果 乔布斯 谷歌 iPhone Android <br />
<br />
<br />
乔布斯与用户邮件往来<br />
<br />
　　北京时间4月21日早间消息，据国外媒体昨日报道，苹果CEO史蒂夫・乔布斯(Steve Jobs)在一封回复用户的邮件中表示，苹果有责任让iPhone远离色情，并声称想下载色情内容的消费者应该去购买Android手机。<br />
<br />
　　近来乔布斯一直在回复用户的各种质疑邮件。上周曾有用户发邮件，质疑苹果拒绝网络作家马克・菲奥里(Mark Fiore)的政治漫画应用一事，乔布斯表示，这的确是苹果犯下的一个错误。最近，另一位苹果用户给乔布斯写邮件，质疑苹果在审批应用过程中充当的道德警察角色。乔布斯对此回复称：“我们的确认为，自己在道德上有责任让iPhone远离色情。那些想要色情内容的人应该买一部Android手机。”<br />
<br />
　　如果这封邮件是真实的，这并不是乔布斯第一次把Android和色情联系起来，并建议想要色情内容的消费者去购买Android手机。本月初，在iPhone 4.0操作系统大会后的问答环节里，乔布斯就公开表示：“你们知道，Andorid平台有个色情商店，你惟一能下载的就是色情内容，别无他物。你能下载，你的孩子们也能下载。那是我们不会涉足的地方――我们不会这样做。”<br />
<br />
　　据业内人士表示，事实上苹果和谷歌(Google)正处于手机平台大战的初期，因此乔布斯显然会继续站在道德高地上不断打击谷歌。<br />
<br />
　　附：乔布斯与苹果用户邮件来往全文<br />
<br />
　　苹果用户马修・布朗宁(Matthew Browing)写道：<br />
<br />
　　史蒂夫：<br />
<br />
　　朋友们多年来一直试图说服我使用苹果产品，但直到iPhone 3G发布后，我才最终转投苹果门下。从那以后，我购买了4部iPhone、两台电脑、几个路由器还有一些其它产品。不幸的是，我真的开始对苹果公司的哲学有点问题。似乎苹果越来越多地为消费者做出选择，决定他们应该看到哪些内容，不应该看到哪些内容。比如，拒绝马克・菲奥里(Mark Fiore)的漫画应用(出于政治讽刺原因)，或是屏蔽任何苹果认为是色情的内容。<br />
<br />
　　我完全同意要让孩子们远离色情，我也完全同意要让我自己远离这些内容。但这就是家长控制能派上用场之处。把这些应用放在一个类别里，然后让家长能自由选择屏蔽这些应用。<br />
<br />
　　苹果的责任不是充当道德警察――而是设计、生产非常酷的电子设备，让消费者能随心所欲地使用这些设备。<br />
<br />
　　感谢你的倾听，<br />
<br />
　　马修<br />
<br />
　　乔布斯在回复中写道：<br />
<br />
　　菲奥里的应用将在近期进入应用商店。这是一个失误。但是，我们的确认为，自己在道德上有责任让iPhone远离色情。那些想要色情内容的人应该去买Android手机。(晓彬)</div>

							
							
							
							
