													<h2>乔布斯承认错杀普利策奖得主iPhone应用</h2>
						
						
																											<div id="postmessage_663787" class="t_msgfont">苹果公司CEO史蒂夫・乔布斯（Steve Jobs）近日在一份邮件中承认，苹果App Store拒绝普利策奖获得者马克・菲奥里（Mark Fiore）的iPhone应用“是个错误，正在修正之中”。<br />
漫画家菲奥里近日获得了普利策漫画创作奖，成为首个获此殊荣的网络漫画家。不过在接受哈佛大学尼曼新闻实验室采访时，菲奥里透露，他去年12月为苹果 App Store开发的iPhone应用NewToons未能通过审查，因为其应用包含讽刺政治人物的漫画。<br />
<br />
查看:普利策得奖者遭苹果闭门羹<br />
<br />
苹果方面称其应用违反了iPhone“开发者项目授权协议”3.3.14条款的规定，即“如果应用程序包含任何根据苹果的合理判断，可能会引起反感的内容或材料，如淫秽、色情、诽谤内容性内容，苹果可以拒绝该应用。”<br />
<br />
菲奥里获得普利策奖之后，苹果邀请他再次提交应用。菲奥里也接受了这一建议并在周五早间重新提交了应用，但目前还在等待审批。<br />
<br />
苹果公司审查App Store应用的政策引发了多起争议。苹果二月份决定移除所有“过于性感的内容”，<br />
<br />
还曾暂时拒绝摇滚乐队Nine Inch Nails的应用，著名喜剧连续剧《南方公园》的应用也一直被拒之门外。在2009年8月，苹果也同样拒绝了一个名为《自由时代》（Freedom Time）的政治主题应用，该软件用于倒数时任总统布什下台的时间。据报道，乔布斯向《自由时代》的开发者发电邮解释说该应用“可能会招致（苹果）大概半数的用户的反感”。<br />
<br />
一直以来媒体都担心苹果对iPhone和iPad上可下载内容的限制过于严格，而像《自由时代》和NewsToons这样的应用程序遭到拒绝似乎将增加这一方面的忧虑。正如《连线》杂志的Brian X. Chen在二月指出，随着苹果电子产品种类增加，苹果将对电子内容的出版和销售享有过大的控制权。</div>

							
							
							
							
