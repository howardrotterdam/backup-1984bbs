																											<div id="postmessage_392497" class="t_msgfont">这是我阵亡两只兔子后得到的关于养兔子的的心得，绝对本人原创。<br />
<br />
一，从街上20块钱买来一只宠物兔后，回家了你得先给它准备一个家，我的很简单，一大些的纸箱，底下铺上旧衣服，衣服上铺几层报纸，兔子每天要排泄，报纸要每天更换，需要的话得一天换好几次。兔子窝和旧衣服要经常在太阳下晾晒杀菌。<br />
二，不要老是把兔子装在箱子里，有空就带它下楼散步，顺便吃吃楼下的青草，身体才会健壮。<br />
三，生菜不能吃，一吃就拉稀阵亡鸟。<br />
四，小白兔最喜欢的不是萝卜，而是有叶子的野草。此外，新鲜馒头，饼干，蛋糕，莴笋叶子，青菜叶它也都很喜欢吃。<br />
五，所有给兔子吃的菜叶都不能沾有生水，一定要是干的，有水要晾干才能喂食，否则会拉稀死亡。<br />
六，两周给兔子洗一次澡，一定要在有太阳的正午，用沐浴液即可，切记不能洗到眼睛和嘴巴，沐浴液洗完要冲干净，完了用干布擦掉毛上的水，用吹风机给兔子吹吹，让他自己在太阳下晒干即可。<br />
七，要想兔子长的好，一定要多吃草。我每天都摘一大袋野草喂它的。兔子的食量很大。</div>

							
							
							
							
