																											<div id="postmessage_393868" class="t_msgfont">养虱子心得<br />
这是我阵亡N只虱子后得到的关于养虱子的的心得，绝对本人原创。<br />
<br />
一，从街上1块钱蹭来几只宠物虱后，回家了你得先给它准备一个家，我的很简单，一大些的脑袋，头发上下铺几根稻草，发丝上沾几滴奶油，虱子每天要排泄，奶油要每天更换，需要的话得一天换好几次。虱子窝和干稻草要经常在太阳下晾晒杀菌。<br />
二，不要老是把虱子装在稻草里，有空就带它趴趴发根，顺便吃吃发根的头皮，身体才会健壮。<br />
三，洗发水不能吃，一吃就拉稀阵亡鸟。<br />
四，小虱子最喜欢的不是奶油，而是有血管的头皮。此外，新鲜血液，饼干，蛋糕，奶油，头皮屑它也都很喜欢吃。<br />
五，所有给虱子吃的头皮屑都不能沾有洗发水，一定要是干的，有水要晾干才能喂食，否则会拉稀死亡。<br />
六，两年都不要虱子洗一次澡，一定要在有雨的阴天，用雨水冲淋即可，切记不能洗到眼睛和嘴巴，冲完要不能冲太干净，完了不能用干布擦掉毛上的水，不能用吹风机给虱子吹吹，让他自己在太阳下晒干即可。<br />
七，要想虱子长的好，一定要多吃血。我每天都扒一大坨血喂它的。虱子的食量很大<br />
