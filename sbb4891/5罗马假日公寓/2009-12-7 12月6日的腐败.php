&nbsp; &nbsp;&nbsp; &nbsp;12月5日，我收到电话，得知聚会的地点为四季渔乡，立刻用Google搜索地址。发现和我的宿舍距离有800米之后，决定懒的去考察，打了一个电话和eric聊了一下啊。于是睡觉。<br />
&nbsp; &nbsp;&nbsp; &nbsp;12月6日，阳光似乎不错，天气18度左右，我打了一个电话给四季渔乡。他们说没有独立房间给我们吃饭。我于是健步如飞一直走到四季渔乡。确认了一下之后坐在厕所位上。无聊的打了几个电话，11点半，上穿格子衬衫，下穿牛仔裤的dacenke出现。此时，酒楼人头涌涌，声音嘈杂。去了四楼，发现有房间，于是开房了（不是搞基哦）<br />
&nbsp; &nbsp;&nbsp; &nbsp; 在我的指引下，一身西装的Eric到了，小肚子笑得一颤一颤的和大岑客握手，略寒暄之后聊起八卦来，什么逸夫书店的历史啊，国家大事啊之类，过了一会儿在电话的指引下，麦园出场，穿着宽松休闲的衣服，眯着眼睛。坐下了。大家进行了友好密切的介绍和会谈……上菜了终于上菜了，说起来这家酒楼的服务真的不怎么样，上一个菜n久，饭也是的。不过排骨听好吃的，肥而不腻，我出去，吼了几声服务员姗姗来迟。终于把菜上全了。麦园来了就聊一下小组的事情。之后billygoat到，黑色带着啥啥（宽袖？）的大衣，比较典雅，长发披肩，面如月，还是很有看头的。可惜我们几个男的边吃边聊，台上的菜肴所剩无几。啊才四个菜而已，长期做体力劳动的我吃不饱啊吃不饱。大岑客又要付账哦，我们纷纷表示要aa。最后还是他请了。<br />
&nbsp; &nbsp;&nbsp; &nbsp;由于酒楼生意过于火爆，我们决定另找地方一聚，下二楼，见拉丁餐厅。于是进去吃吃自助餐吧，之后哦我们后悔了，餐厅音乐大，好吃的东西在中午被人一扫而空，餐厅人少，灯光暗。那位女孩脱掉大衣露出黑色的长袖衫，加上贴身的牛仔裤，真是婀娜多姿，她扫了扫头发，开始谈起了高难度话题，基本上都是我，麦园，大岑客和女孩讨论这些哲学，政治法律话题，什么要有系统思想什么道德经什么跨省之类的。麦园回答了哲学问题，我聊了一下焚烧厂和部分动漫，大岑客把法学都抖落出来。这女孩真是如狼似虎，求知欲求不满，业务员只是迎合一下。4p的结果是女孩眉飞色舞说了一下午然后一句4点有事穿上大衣扬长而去。我们如同被榨干的药渣，继续聊着没营养的话题，大概觉得这个餐厅灯光越来越暗好像没有意思于是转移阵地，出来到了江南西。并且去迎接草你驹sama。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;江南西一如既往的热闹，大岑客突发奇想，说出“星巴克”三字，我心想这里有什么星巴克。黑线完后想起了一家西餐厅“喽岑脚”（烧伤脚）也叫“绿茵阁”的奇特词汇。跨过马路上了绿茵阁，这真的是腐败啊。我们为了节省一下，不打算吃东西，而是叫了一些饮料和雪糕，话说咖啡真苦，朱古力也很苦，我消费50多元来喝了这么有品位的饮料果然是自讨苦吃。我们拍照，互相揶揄。eric和草你驹交流其业务员的艰辛。草你驹和大岑客聊起理财投资。我和麦园有一句没一句聊着。服务员来了，被eric小小调戏了一番。此时麦园没点东西。Eric要加了不少前缀的柠檬茶，草你驹要了雪糕船，大岑客喝了有修饰语的茶，我自讨苦吃。大家合影。麦园戴上墨镜非常和谐。西餐厅黑乎乎的带什么墨镜呀混蛋……忘记描述餐厅，音乐很轻灵，灯光很情调，沙发很柔软舒适。服务员很会推销食物，但是我们都知道其实最后还是吃公务员的所以没有点东西。<br />
