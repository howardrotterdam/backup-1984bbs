													<h2>原来同姓，不过极陌生</h2>
						
						
																											<div id="postmessage_203687" class="t_msgfont">河南好人许志永其人其事<br />
<br />
作者:网络&nbsp; &nbsp;媒体报道&nbsp; &nbsp;2009-7-18 <br />
<br />
<br />
河南好人许志永其人其事 致许志永君兼转帖按语： <br />
<br />
&nbsp;&nbsp;志永君你好！得知公盟因公权力的滥用而遭遇到的可耻算计之消息后，我除了气愤之外，一时半会儿还想不到该为你与公盟做点啥的好办法来；于是，在昨天中午得知你的那篇现已从你博客上蒸发掉了的文章发表消息后，我便开始在好几个微博上转发了你的文章摘录与你的博客网址。经我宣传过后，很快，就有你的一个山东粉丝（也是我的牛博读者），表示愿意为你和公盟捐款以示声援；尽管她对公盟不甚了解，但是，她说她信任你，也绝对支持你。我的一个香港好友，也在网络上发起了为公盟捐款的倡议。此外，我还在我自己的博客上，转了一个网友的一篇写你的文章；但是，我却把原标题更换为《河南好人许志永其人其事》了。下面的网址，是我在我博客上发表那篇转帖的地方：<a href="http://blog.sina.com.cn/s/blog_48fceb150100eww0.html" target="_blank">http://blog.sina.com.cn/s/blog_48fceb150100eww0.html</a> <br />
<br />
<br />
<br />
<br />
2003年3月，大学生孙志刚在广州收容所被殴死，经互联网传播，酿成全国广泛关注的公共事件（参看孙志刚事件）。同年5月14日，许志永联络同为法学博士的俞江、滕彪，向全国人大常委会法制工作委员会递交建议书，指出当时仍在施行的《城市流浪乞讨人员收容遣送办法》违宪，要求启动违宪审查。是为中国司法史上里程碑式的“三博士上书”事件。一个月后，《城市流浪乞讨人员收容遣送办法》被废除。 <br />
<br />
<br />
2003年5月29日，思想开明、与法学界、知识界自由主义分子有密切联系的河北企业家孙大午被地方法院以非法集资的罪名拘捕、指控，因此案被认为具有浓厚的地方政府打击报复异议分子的性质，被告获得知识界、舆论界和当地农民的广泛同情和支持，许志永志愿担任孙大午的辩护律师，展开了艰苦卓绝的法律援助，最终被告获得缓刑判决，避免了牢狱之灾。 <br />
<br />
<br />
2003年7月，许志永与张星水律师共同发起“阳光宪政科学研究中心”，后改名为公盟，作为推动公民社会和法治社会建设的民间团体，发挥卓有成效的影响力。 <br />
<br />
<br />
2003年11月，许志永以独立候选人身份竞选海淀区第十三届人大代表，获得了所在选区选民的强烈支持，高票当选，成为北京市第一位以独立候选人身份当选的区人大代表，并于4年后的2007年再度竞选成功海淀区第十四届人大代表。通过竞选人大代表的举动，许志永证实了中国法治的实质性进步。 <br />
<br />
<br />
2004年2月，为了声援媒体的良知和责任，许志永接受邀请作为《南方都市报》总经理喻华峰案的代理人之一。在3月19日喻华峰和李民英分别被判处12 年和11 年徒刑而同一天程益中被捕的危急情况下，公盟全体成员参与此案，郭玉闪等通过网络揭露事实真相，参与发起网络签名；张星水律师组织专家论证会等一系列活动。在成千上万有良知的中国公民的呼吁下，尤其是在任仲夷、吴南生等一批退休老同志的坚定声援下，2004年6月，广州市中级法院二审改判喻华峰8年，8月，邓海燕和程益中先后获得释放。 <br />
<br />
<br />
2004年上半年，许志永关注艾滋病问题，组织北京动物园搬迁事件的研讨，并向有关机构呼吁，对阻止北京动物园搬迁有所贡献。下半年，许志永到美国去观察和研究选举。他做了民主和共和两党的志愿者，做一些基层而实际的工作，积累了相当的经验。 <br />
<br />
<br />
2005年，许志永开始关注信访问题，访问北京“上访村”，并去国务院信防局所在地观察体验“截访”现象，为阻止地方截访人员对上访群众施暴，与之发生冲突。最终导致上层干预，短期制止了暴力截访的做法。同一年，许志永用大量的时间在做“承德案”。这是一个四位农民先后被判过五次无期徒刑或死刑的奇特大案。但是，以许志永和他的同伴们的调查和掌握的证据，他们是无辜的。他甚至找到了真凶的相关线索，已经提交给了司法部门，要求复查。直到目前，许仍在为此奔走。 <br />
<br />
开办网站关注动物园搬迁，质疑财政预算，监督人大常委会，支持竞争性选举…… <br />
<br />
<br />
２００４年海淀区政府有关部门和环保人士提议动物园搬迁，许志永和其他代表一起组织了一次论坛，邀请中国科学院、工程院的几位院士，邀请市人大代表、专家、律师，还邀请中小学生在分会场参与讨论。大家一致认为动物园的搬迁是涉及到北京甚至全国人民的公共福利的大事情，应该经过法定程序来决定；同时还认为这个大项目耗资巨大，如此重大的事情应该通过人大来决定。许志永等热心代表还开办了一个名为动物园的网站，决定一直关注下去。网站关注，这算是代表履职的新手段！ <br />
<br />
<br />
许志永认为目前人大对财政的监督做得很不够，不知道政府究竟怎样花纳税人的钱，不知道这钱花得对不对。在２００４年１月的海淀区人代会上，海淀区的财政预算中有个“其他”项目是１２．６亿元，而当年海淀区的总财政收入才５０多亿元，不少代表在讨论中提出了这个问题，许志永在大会上正式提出质疑，要求对“其他”详细解释，他同时把这个情况提交给媒体进行监督。区财政局于是很认真地解释，详细列明了该项目用于扶持一些科技企业等。许志永说：“代表应该树立监督的意识，学习相关知识或者聘请专业人士详细考察政府的财政支出是否合理。把人大对政府的财政监督落到实处，审查钱该不该花，为纳税人看好每一笔钱。” <br />
<br />
<br />
许志永说，热心的人大代表组成了小组，每次有两个代表去列席海淀区人大常委会，会后把开会的相关情况复印散发给其他代表，“我们在监督人大常委会”，许志永笑着说。记者问他们的做法是否遭遇否定，许志永说他们和人大常委会协商以后获得了人大常委会的同意，而且在常委会的“制度创新”总结中列入了“人大代表列席人大常委会”，保证每一次人大常委会有两名普通代表进行监督，负责记录会议出勤情况、讨论的具体问题等事项，并将记录通报常委会和全体代表。 <br />
<br />
<br />
许志永当选代表以后，一个居住在北太平庄的选民因没有领到低保而找到了许志永，许志永当即向街道办咨询，转达了该选民的意愿，后来问题得到解决。许志永说：“代表的基本工作就是倾听选民声音，把他们的意见和建议转达给政府和人大。选民选了我，我就应该给选民做些事情。”为此，今年５月许志永设立了代表工作室。 <br />
<br />
<br />
许志永本人是自荐参选，通过“竞争性”选举当选的，他非常赞成代表选举或者官员选举中的竞争，他说：“人大代表履职问题根源于选举，选民挑选出最愿意当代表、最能干、他们最信赖的人当代表，这样的代表一定会有更强烈的责任意识。” <br />
<br />
<br />
许志永表示，今年还将以公民的身份建议北京市人大在修改选举的相关法规时把“选民登记地点选择权，宣传活动保障，预选程序”等内容加进去。选民应该有权在社区进行投票，目前的竞争性宣传多遭遇阻止，往往是城管以保护环境为理由来制止，许志永说：“宣传如果不当也可以约束，但应该是在尊重选举权和被选举权的前提下的约束。”他认为现有的《选举法》对预选规定得还不够，北京市的法规应该细化。 <br />
<br />
<br />
结束采访，记者与他告别，想起他在美国时写的日记：回到中国去，那是我的家，那是个充满希望和梦想的地方，那里有沉重的责任，有一生的幸福和骄傲。 <br />
