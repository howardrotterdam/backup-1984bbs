													<h2>回复 2楼 gjlawyer 的话题</h2>
						
						
																											<div id="postmessage_203683" class="t_msgfont">两个都是找不到服务器<br />
----------<br />
<a href="http://tieba.baidu.com/f?kz=623445195" target="_blank">http://tieba.baidu.com/f?kz=623445195</a><br />
<br />
新闻人物：维权律师许志永<br />
<br />
河南好人许志永其人其事 记者： 张楠<br />
北京 Aug 6, 2009<br />
&nbsp;&nbsp;<br />
最近被警方拘留的“公盟咨询公司”法人代表许志永是中国著名青年法学家、宪政学者和公民维权的领军人物。认识他的人说，许志永是一个理性、执着的人，希望尽自己的微薄之力推动中国的法治建设。<br />
<br />
许志永1973年生于河南省民权县。据说，他很为此自豪，曾说“注定我这辈子就是为民权而奋斗。”<br />
<br />
许志永的同事和朋友把他称为执着追求理想的人。河南维权人士汪海洋说：“我印象最深的就是（他有）法律的精神、法治的理念和他的美好愿望。他的美好愿望就是想把这个国家，通过法治的手段，使人人得到一种平等的社会地位，铲除人压迫人、人欺辱人的现象。”<br />
<br />
许志永毕业于兰州大学法律系，先后获学士和硕士学位。1999年，入北京大学法学院学习，2002年获法学博士学位。他现在是北京邮电大学讲师，海淀区人大代表。<br />
<br />
*“三博士”上书*<br />
<br />
许志永首次走入公众视野是在2003年的“三博士”上书事件中。那一年，大学毕业生孙志刚在广州收容站被殴打致死。之后，许志永与另外两名法学博士俞江、滕彪联名上书全国人大常委会，要求对《城市流浪乞讨人员收容遣送办法》进行违宪审查，废除收容遣送制度，对废止该办法起了推动作用。<br />
<br />
许志永的北大同学滕彪回忆说：“我和许志永上博士的时候，就已经开始讨论相关的问题，劳教制度等等。孙志刚事件发生之后，我们就很快地形成了一个一致的看法。”<br />
<br />
*创办公盟*<br />
<br />
而后，许志永和俞江、滕彪等人共同创办了公盟。公盟本是一个公益性法律援助组织，但在中国很多公益组织很难注册为“公益机构”，只能到工商局去注册为“公司”，公盟不得不在“公司化生存”中从事公益事业。<br />
<br />
五年来，公盟做了大量工作，包括向全国人大递交宪法ren&nbsp; &nbsp; quan条款的修订建议；关注河南艾滋村孤儿院被政府强行关闭事件；为《南方都市报》总经理喻华峰和主编程益中辩护；进行中国信访制度研究；帮助黑砖窑受害人提起行政诉讼；援助毒奶粉受害人；发起北京律师协会直选活动等。<br />
<br />
滕彪认为许志永是公盟的灵魂人物。他说：“他还是很有威望。公盟是一个非常团结的团队。虽然有些人比他年纪大，有些人的经验更多，但是许志永一直是公盟的最核心的灵魂人物。”<br />
<br />
*为民受苦*<br />
<br />
为了撰写《中国信访报告》，许志永曾在北京南站的“上访村”住过两个月，还曾多次探访京城内关押上访民众的“黑监狱”，并受到不法分子的殴打。<br />
<br />
河南维权人士的汪海洋说：“许志永博士奋不顾身地到这些地方和他们理论，当然是非常理性地理论。看守打他，他就让他打。‘你打，你打吧。’最后因为他正气凛然，使原来打过他的人见了他就望风而逃。所以这种精神，我觉得，在中国人来说，太少见了，也太可贵了。”<br />
<br />
广东律师庞琨参加了今年四月公盟举办的针对信访和群体性事件的法律培训。他回忆了许志永讲课时的情景。<br />
<br />
庞琨说：“他就讲到，去维权的时候，首先你要带着一种受苦的心态去，为老百姓受苦、为权益受苦的一种准备。(他)甚至受到过别人的攻击，而且始终是面带微笑，用一种从容和一种怜悯的姿态去看待那种暴徒的方式。我觉得这是我们一般普通人做不到的。”<br />
<br />
许志永非常关心访民的疾苦，为他们付出了大量时间和精力。访民代表曾送锦旗给他，上书“书生报国无它物、唯有手中笔如刀”。<br />
<br />
*“人格高尚”*<br />
<br />
许志永的生活十分简朴。北京律师杨慧文说：“他被采取了强制措施之后，我们去帮他整理东西，去了他家里头，才发现他家里的生活用品非常地简单，简陋得让我都觉得有点受不了。这从另一方面也能体现出来，他经常吃工作餐的时候，都是按八块钱的标准来吃的。”<br />
<br />
接受记者采访的人都对许志永的人品和人格给予高度评价。<br />
<br />
“非常坦诚，从来不会有什么阴谋诡计，跟他在一块儿有一种信任感、有一种安全感。”<br />
<br />
“这个人虽说年轻，但是我觉得人格非常高尚，人品非常伟大，知识非常渊博。”<br />
<br />
“最强烈的印象就是他特别地纯粹，非常地热心做公益的事。”<br />
<br />
“做人非常谦卑，是一个非常理性的人。”<br />
<br />
*公盟筹款救人*<br />
<br />
但是这样一个人现在却面临牢狱之灾。7月14号税务部门发出通知，对公盟追缴所得税和罚款共142万元。7月29号清晨，许志永被警察带走。<br />
<br />
许志永的同事和朋友坚信他“没有逃税的故意。”但公盟还是决定接受社会各界捐款，希望能还清行政处罚开具的数额巨大的税款和罚款，使许志永得以获释。<br />
