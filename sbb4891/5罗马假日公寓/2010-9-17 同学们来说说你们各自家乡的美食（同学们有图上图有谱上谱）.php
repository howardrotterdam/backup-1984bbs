													<h2>同学们来说说你们各自家乡的美食（同学们有图上图有谱上谱）</h2>
						
						
																											<div id="postmessage_833908" class="t_msgfont">家乡的美食太多了，三日三夜也说不完呢。咸鱼大叔先来说一道《菠菜蛏子羹》<br />
<img src="http://thumbsnap.com/i/8OqXBl9R.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
小时候，亲朋好友之间若有红白之事，总会呼朋唤友摆上十几桌，吃上三天三夜，或者一星期，如果某家人丁兴旺交际甚广，摆上几十桌上百桌也不是不可能。<br />
<br />
远亲近邻皆来帮忙，人越多，意味着这家越有面子。吃的规格也从普通家常菜到平时不舍得吃的山珍海味不一而足，琳琅满目。客人吃的过瘾，主人家也挣足了面子。<br />
<br />
不过餐桌上的大鱼大肉山珍海味，吃多了自然腻味，第一天吃的津津有味，第二天偶尔食之，第三天早就视而不见，客人们更多是把筷子伸向了蔬菜和各种羹菜。<br />
<br />
不过大人总是要多一点心计，知道留好肚子，细水长流，好的次的，每样吃一点，绝不一次吃倒了胃口，所以知道了窍门的大人总会保留着肚子里一块空间，直到聚餐的最后一天，因为最后一天一定会有更意想不到上品菜出现。<br />
<br />
小孩子自然管不得那么多，看见螃蟹吃螃蟹，看见大虾吃大虾，看见鳗鱼吃鳗鱼，看见踢膀吃踢膀，大鱼大肉吃多了自然想喝点汤汤水水，这时候如果有各种各样的羹上来，自然一把抢到跟前来，猛吃猛喝，接下来再也没有胃口和空间吃得下其他好东西了。<br />
<br />
这时候大人总会给自己的孩子一个爆栗子，骂上一句“活灵不生，只会饕羹”，意思就是没有一点脑筋，满桌的好鱼好肉不吃，只知道吃这些不值钱的羹。当然这其中也包含了一种戏谑和调侃的意思，还说明了这些羹确实是鲜美无比，满桌的大鱼大肉都无法阻挡人们对羹的喜爱。<br />
<br />
咸鱼今天要做的这道菠菜蛏子羹也是极其普通，制作也非常简单。<br />
<br />
买上一把菠菜，一斤蛏子，这里要特别提醒一下，蛏子最好选粘满了泥浆的那种，因为这种蛏子养在滩涂里，新鲜而且味道更鲜美，更重要的是这种蛏子吐过泥后，肚子里不会留沙子，而有的蛏子看起来干净，却是养殖在沙滩里，肚子里沙子非常难吐净。<br />
<br />
蛏子不必一次用完，你可以做成蒜蓉，白焯或腌制，方法非常之多。<br />
<br />
把蛏子先过沸水，去壳备用。<br />
<br />
锅里少许油，放蒜末和花椒呛锅，马上放进拦腰切断的菠菜翻炒几下，放进蛏子，倒水没住菠菜为准，快速煮开，放盐、鸡精、胡椒粉，勾芡，可稀可浓，一碗鲜美无比的菠菜蛏子羹就好了。<br />
<br />
本菜一定要快，菠菜不可多煮，否则容易失去口感。另外菠菜根也不必摘除，菠菜根有点甜，非常好吃。<br />
<br />
朋友们值得一试。<br />
