																											<div id="postmessage_834659" class="t_msgfont">我最近常做嫩生姜炒肉。<br />
材料：鲜猪肉、嫩生姜、蒜、料酒（酒质要好）、一小撮霉干菜。<br />
要求：嫩生姜切成宽大片状，霉干菜可做稍微清洗。<br />
做法：先烧油起微烟，放入猪肉爆炒，再放入霉干菜，这时要求火力要猛。待肉质变色（这时霉干菜会使猪肉变成浅酱色）起肉香之后，放入料酒、嫩生姜和蒜，为避免烧焦，可放入少许水。之后微火焖盖慢炖。5分钟之后起锅（可放少许味精），肉香扑鼻。尝之，肉质鲜嫩，生姜可口，为一世名菜。</div>

							
							
							
							
