													<h2>台湾宗教人员对器官捐赠的看法</h2>
						
						
																											<div id="postmessage_747267" class="t_msgfont"><a href="http://lxrzr.blog.hexun.com/10722041_d.html" target="_blank">http://lxrzr.blog.hexun.com/10722041_d.html</a><br />
<br />
【道教对器官捐赠的看法】 <br />
<br />
道教高忠信先生－台湾道教总会理事长 <br />
<br />
&nbsp; &nbsp;『尊重生命、捐赠器官』可以延续另外一个有用生命的继续生存，对社会新的价值观而言，是一个值得倡导和鼓励的理念，本质上和宗教兼善天下、福利苍生的大爱是不冲突的。不过，因为各宗教的宇宙观和生命观不同，因此，在教义的规定上，对身体和生命的观点就不一样，而社会上的一些传统思想，也是推动此一活动首需突破和克服的。<br />
<br />
&nbsp; &nbsp; 在我国传统孝道思想之中，深信『身体发肤，受之父母，不可毁伤』的观念，是根深蒂固的，因此对遗体的处理十分慎重。在道教的生命观而言，认为人类和万物，同属宇宙大自然的一部份，生命的组成就是『精、气、神』，精是父母精血所结成的形和质，这就是我们的肉体和骨骼，再母体是一月为胞，二月为胎，到三月时依年份的不同，分别由北斗某一星座，下降一股阳气灌注胎中，称之为『魂』，也就是个人的本神，至四月时又有来自地上的阴气化成七魄，合体于包胎之中，至九月时期满神布即可发声，十月落地成为一个新的生命。这时不再透过母体输送大气，开始由其本身吸取自然之气，经过生、壮、老直至气散而结束人间的生命，自此每七日散离一魄，四十九日七魄散尽，魂魄分离才是生命的真正终点。道教认为，人的生命来之不易，而有「贵命重生」的思想，因此道教徒以保存生命和身体为终身的职志，用内外功德作为延续生命的修持方法，冀能修成精、气、神三者的永远合一，以达到天地人三者合一的目的，也因此而希望人人都能无欲无私、不争不夺，去世累、免烦恼，快快乐乐的留存在现实的人间世界，因此对身体的保存特别重视不容损毁，而且人「死不欲速朽」的观念而主张土葬。不过，时代和科技是进步的，为了贯彻与道教理念相符的『生命的意义，在创造宇宙继起之生命；生活的目的，在增进人类全体之生活』的崇高宇宙观和生命观，我们愿意将道教教义做更高层次的升华，先行呼吁一般的信众，以遗爱人间，积功累行的理念来向应这个号召，然后再在道义的调适上，来肯定捐赠器官的生命价值。<br />
<br />
【回教对器官移植的看法】<br />
<br />
回教武宦宏先生－台湾回教协会理事长 <br />
<br />
&nbsp; &nbsp;回教不许可伤害亡故者的遗体，折断亡者的骨骼就等于折坏活人的骨骼一样，但学者们已同意死者之器官可以移植给患者，因为这是不伤害遗体的行为，而是某一型态的行善。<br />
<br />
&nbsp; &nbsp;条件是：本人生前业已同意，或亡故后其家属予以同意，本人生前箬笠下书面同意书则最佳。<br />
<br />
&nbsp; &nbsp;有些器官，如肾脏、骨骼等，可以在健康时移植，但必经本人同意，以救他人之性命，这是最好之善行。<br />
<br />
&nbsp; &nbsp;但是已婚妇女，如未经其丈夫完全同意，不可在生前捐一肾或其它器官，因妻子的健康关系丈夫甚巨。<br />
<br />
&nbsp; &nbsp;切除身体某一部份，只要是：（1）医学上的原因 （2）防止健康恶化，均属可行。<br />
<br />
&nbsp; &nbsp;器官移植之捐赠者及受惠者，不因信仰不同而受限制。<br />
<br />
&nbsp; &nbsp;不可有商业行为（出卖器官）。<br />
<br />
&nbsp; &nbsp;将动物器官移植活人身上以救命。<br />
<br />
 【从基督教看器官捐赠】 <br />
<br />
基督教周联华先生－基督教二千年福音运动主席 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;在耶稣基督的时代，根本没有器官捐赠这一件事，所以在圣经上找不出根据；但是看到耶稣愿意把自己的身体完全献上，让人把自己钉在十字架上，为救赎众人，他对器官捐赠应该是正面的。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;也许某些基督徒认为把器官捐给了别人，将来复活的时候，怎么办？<br />
<br />
这种顾虑是不必要的，因为复活是上帝的大能，也能从无创造有，复活时候难道要完整的吗？更何况人死以后早已腐烂，而复活身体为实体，已经超越了人间的肉体。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;更有些基督徒害怕，如果我的器官捐赠出去，到某人身上，他将来去犯罪，或拜其它神明，怎么办？也不必过虑，因为我世界生命已经结束，某部分器官捐给另一人，是他责任的，与我无关。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;所以，基督徒的看法对器官捐赠，从我的观点来看是正面的。 <br />
<br />
【从天主教的观点看器官移植】 <br />
<br />
天主教狄刚先生－天主教台北总教区总主教 <br />
<br />
（一）天主教对「生命」的看法：「天主是爱」，天主爱人，给人生命，愿人生命乃是天主给人的最大恩赐。人终其一生就是为了爱而生活。这是天主依其肖像造人的意义。人要自爱，也要爱人。自爱必自重，爱人必重人。天主令人爱主爱人二者不可缺一。尊重生命，爱护生命，必设法保全生命，发展生命。天父救人爱人以牺牲自己的生命为爱的最高境界，并令圣子为人舍生，令我们则效。捐赠器官乃爱人的高度表现，生时教会以鼓励教友捐赠器官，更何况身后！<br />
<br />
（二）全尸问题：天主教没有问题。人在末日要复活永生，像救主基督一般。善人在复活时将获得一全新而能不朽的身体。这新体由全能天主创造。天主教现在也容许火化。&nbsp;&nbsp;<br />
<br />
（三）天主教对中华民国器官捐赠中心（协会前身）、董氏基金会举办尊重生命系列活动，十分钦佩，愿随社会各界，共襄盛举。虔祈上主赐福助佑，使这一系列活动蔚为风气，造福社会大众。 <br />
<br />
【佛教对器官移植的看法】 <br />
<br />
佛教释证严－静思精舍住持、慈济功德会会长<br />
<br />
&nbsp; &nbsp; 非常敬佩台湾器官捐赠中心、董氏基金会伟大的呼吁。器官捐赠的观念不是自今使，器官捐赠的观念，早自二千多年前，释迦牟尼佛时代就有。佛陀讲经说法，无不以利益众生为怀，六度万行也是以布施为首。布施功德无量无边，备受赞颂。<br />
<br />
&nbsp; &nbsp; 早在十一年前，证严在台北讲述药师经，就积极推行器官捐赠、遗爱人间的活动，当时却也获得极大的回响，慈济委员与会员到法院公证『器官捐赠』者，不在少数。<br />
<br />
&nbsp; &nbsp; 器官捐赠的观念，在台湾所以不能广为推广，主要原因是：中国人都有死后全尸的观念，往生者之亲属，不忍把往生者的器官割舍，故吝于捐赠。其实，我们的身体只不过是四大假合而成，缘聚则生，缘散则亡，所以历代禅师都说我们有形的身体，只是一具臭皮囊。这具臭皮囊能发挥功能时，就是至宝，不能发挥功能时，何异废物。<br />
<br />
&nbsp; &nbsp; 释迦牟尼佛在世时，特别倡导施医施药、救济病苦孤贫，药师中阐述十二大愿，愿愿都在于『皆令众病悉除，身心安乐』。<br />
<br />
&nbsp; &nbsp; 菩萨行布施有三种：下布施是以饮食粗物，软心布施；中布施是以衣物宝物布施；上布施是以头目血肉，尽用布施。行下布施易，行上布施难，靼行上布施之功德数倍于行下布施，理所必然也。<br />
<br />
&nbsp; &nbsp; 布施的功德很多：如行布施可以破苦相，与人乐；可以为慈相，济一切；为安稳，不布畏：为立事，聚众缘，为善行，受果种；为福业，善人相；破贫穷，断恶道。能志之死后『器官捐赠』的人，头目血肉都可以布施，何况是饮食、衣物与宝物呢？器官捐赠就是菩萨的上布施啊！<br />
<br />
&nbsp; &nbsp; 我提倡器官捐赠的理由是：第一、励行菩萨上布施，功德无量无边；第二、器官捐赠就是头目血肉，尽用布施，可以得正见；第三、器官捐赠就是善用器官功能，可以延续器官生命；第四、器官捐赠是慈悲大愿的显现，可以延续个人的慧命；第五、器官捐赠可以令病悉除，完成诸佛本愿；第六、器官捐赠风气一旦推展开来，可以对社会大众教以正当知见。<br />
<br />
&nbsp; &nbsp; 大乘佛教的精神，在于利济众生，无私无我，金刚经也一再强调破我执灭我相，法华经更诠释『无缘大悲、同体大悲』的佛陀教育意义，凡是正信、正见的佛教徒都应有励行菩萨上布施苦的精神，何况人死之后的各项器官与其与草木同朽，不如善尽功能在病苦众生上发拔若与乐的作用，这才是佛教徒应有的观念与态度。 <br />
<br />
【天帝教的生命观】 <br />
<br />
天帝教林助雄先生－财团法人天帝教台湾省长院参教长 <br />
<br />
&nbsp; &nbsp; 人类的生命由和子（或称性灵、灵魂、阿赖耶识……，属精神层面）与电子（或称肉体、身躯属物质层面）组成。和子与电子为无形宇宙和有形宇宙所有活动最终成分，他们构成宇宙间万象，包括山川、石矿及各层级动植物，在动物界拥有生命，非动物界则为生机。人为高级性灵由较高级的和子和电子组成，人身之可贵，贵在拥有「精气神」三宝及「自由意志」的灵觉作用。<br />
<br />
&nbsp; &nbsp; 人之死，天帝教称为「回归自然」，电子体（肉体）属后天的有形世界，终究要腐化归于尘土，化成自然界本来已有的各种元素。和子（性灵）部分则依其生前修练情况，归位难当时他所适合修练之处。轻盈又有能量的和子能运用自然，有自主力量，k飞向自己能力所及的环境继续修练，从自由神到天君、圣、天尊、仙、佛等更高境界。无修持之和子则沾满了阴电子，剩下之和子无自主能力，任其在宇宙间漂浮，随着因为自然律任由山川、石矿及至低级生机体或生命体吸入处在悲惨无助的情况，或由于因果律轮回转世继续为着缘、围着业忙碌。<br />
<br />
&nbsp; &nbsp; 在生修练，为求和子（性灵）更精纯、更俱能量，其过程为心的修持，以发挥慈悲、博爱、清心、智慧等功德，即所谓「性功」。其次为电子体（肉体）之修练，使之成为和子的支持者而非对立者，其过程在降低世俗欲望的过度奢求，是为「命功」，而「性命双修」即是天帝教特别强调的奋斗修持法门。<br />
<br />
&nbsp; &nbsp; 既是重在和子修持，又认为电子体终要回归自然化为尘土，人死后自无留恋尘世之必要，因此捐赠器官不只是有助于性功之焙练，又能将电子体作有效运用，尤其同奋（天帝教教徒之俗称与互称）之命功修练（尤其静坐功夫）达上乘时更是有助于受赠者，因为天帝教同奋具有阳刚正气，其电子体中阳电子居多，基于宇宙基本质素之一电子（非世俗科技所惯称之电子）同引异排原理有助于受赠者阳电子增加。<br />
<br />
&nbsp; &nbsp; 人有魂魄，当回归自然后，留有一魄在人间关怀自己拥有过的电子体及其所属环境，由是对受赠者更有潜移默化、激励向上的作用。基于此，天帝教响应支持器官捐赠、遗爱人间，也算本教尊重生命、热爱苍生实质的一面。<br />
