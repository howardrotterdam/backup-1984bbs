																											<div id="postmessage_491484" class="t_msgfont">希望这是个长期有效的活动召集贴。<br />
<br />
有感于老艾家18裸汉，包括我自己，都毫无身体美感可言。人过30后越发觉得健康的重要性，逐渐放弃和那些无谓的熬夜，食无定点的恶行。逐渐让生活平静正常起来，让自己有更多的思考的时间。<br />
<br />
一群自由份子们凑在一起神吹胡聊是每次活动最吸引人的部分，我们交换那些纯粹的话题，分享那些在人群里无法畅谈的话题。<br />
<br />
记得2000年的5月，我们第一次小五台五台连穿的第二天晚上，我们从西台下撤到管理处招待所，一群人刚刚经历中午的暴晒（好几个人脱皮）和下午的暴雨（本来是打算在山上露营的），在招待所刚刚吃上热饭，就开始讨论中国的民主实现是制度优先还是公民教育优先这个宏大叙事来。一直争论到凌晨2点。完全忘记了凌晨4点我们就要再次出发。<br />
<br />
这是有记录来北京户外圈的第一次小五台五台连穿。在我的记忆里，完全没有那几天的艰辛（暴晒，暴雨，大雾，迷路全让我们遭遇上了），却牢牢的记住了那天晚上那场宏大的讨论。那时我们的年纪大多在22-26岁。10年已经过去了。<br />
<br />
是的，我们所需不多，却会收获很多……<br />
<br />
你需要<br />
1.一辆自行车――从买菜的菜车到上万的专业车辆都可以。我相信一两次活动后，只要你还想继续运动下去，你一定会去购置一辆看起来比较专业的单车。预算从2000左右起跳。当然购买一个合适的二手车辆是很多车友入门的首选。<br />
2.周末一点时间――少上会网，少打会游戏，少睡会懒觉。<br />
3.很少的钱――一般就是一顿午餐的钱，或者活动结束后的聚餐。从完全不花钱到花50-60不等。<br />
<br />
收获<br />
1.汗水――我相信很多人没有健康的流过汗水了<br />
2.心境――多与自然接触会少很多虚妄和浮躁之心<br />
3.眼界――车友之中藏龙卧虎，每次活动聚会的神吹海聊才发现某某居然……<br />
4.友谊――回顾十多年来户外的经历，我发现留在北京的理由：在山里结识的朋友是这辈子最大的收获<br />
5.爱情――不得不说，户外圈是交友成功率最高的圈子。最少我身边的朋友大多在圈内终结自由之身。当然，这只能当意外收获，如果刻意为之，我相信你会失望的。<br />
6.健康――哪怕以上5点都落空了，但你还会有一个好身体。<br />
<br />
×××××××<br />
我预计从三月开始每二周会有发布一个活动计划，每次活动一般为当日往返，一般的单日骑行距离在150公里左右。偶尔有2日活动，需在郊区住宿。<br />
<br />
Just do it</div>

							
							
							
							
