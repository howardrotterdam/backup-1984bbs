													<h2>毕业生必须知道：干部身份、三方协议、派遣证</h2>
						
						
																											<div id="postmessage_345715" class="t_msgfont">学校里关于毕业生的会也已经开了一大堆，耳朵也长v了，嘴唇也磨薄了，很多人随大流听听看看，可真正重要的一些东西相信有很大一部分人却没有搞懂。如果不信，你可以问问自己，什么叫三方协议？三方协议和谁签？有什么用？户口和三方又有什么关系？当一个个问题逐一提出来时，你可能已经开始觉得发虚。<br />
&nbsp; &nbsp; 在这里，我想简单的说一说我目前所知道的关于这方面的一些问题，相信肯定有很多不全面的地方，但至少相信可以帮帮你。<br />
&nbsp; &nbsp; 首先是大学毕业后的人生基本规划（本处所指为非在职考研、考博、考公务员等同学）：<br />
&nbsp; &nbsp; 大学毕业后的一年最为关键，举例如2008年6月毕业7月正式工作者，其从08年7月到09年7月这一年是对其人生的基本规划最为重要的一年，原因涉及三项――三方协议、报到证（派遣证）、干部身份。<br />
&nbsp; &nbsp; 关于三方协议：<br />
&nbsp; &nbsp; 什么叫三方协议？网上答案都有，随便查查就知道了。但再深一些说呢，肯定有人不知道了。<br />
&nbsp; &nbsp; 三方协议，简单的讲就是学校、学生本人、工作单位三方就毕业生离校后就业工作落实所签署的一份协议书，很多人知道的是：三方协议过去必须签，但现在不一定，三方签了只是学校统计就业率的一个参考指标。三方和派遣证挂钩，但我没有留京户口，单位签三方就要解决你的户口，所以现在单位都不签三方了，尤其是私企等等。没错，上边说的都对，所以有些朋友们就把三方协议放弃了，而只是找到工作，自己去做个打工者，然后只考虑劳动合同。<br />
&nbsp; &nbsp; 那么究竟三方协议背后究竟还有什么呢？其实三方的背后还隐藏着很多。<br />
<br />
&nbsp; &nbsp; 如果说到三方协议，那么必需提到的就是派遣证与干部身份。<br />
<br />
&nbsp; &nbsp; 三方协议作为国家统计大学生就业率的一个根据，同时也是国家派遣证发放的一个证明。只有你签署了三方协议，拿回学校，学校才会在你毕业后将派遣证发给你，而你拿着派遣证到你工作的单位报道，就此开始计算工龄，而你也就拥有了干部身份（每年基本6月25日毕业，所以6月18日前必需将三方交到学校）。<br />
<br />
&nbsp; &nbsp; 派遣证（报到证）：<br />
<br />
&nbsp; &nbsp; 派遣证一式两份，一份是派遣证，另一部分是报到证。派遣证在你毕业后将放入你的档案，由国家直接打到你的单位（档案属国家机密，不允许个人持有。如果你的用人单位拥有档案保存资格那么你的档案就放在单位，如果没有，那单位会掏钱将你的档案放在人才市场类的档案保存处。你过你没工作，那你的档案就直接打回原籍）。而报到证则交由你手自行保管。在这里必须要重点说的是，很多人在毕业后没多久就把报到证丢了，而当若干年后单位希望将你提干要求出示报到证时，很多就没有了，而只能再跑回某地去重新开证明，这时的证明可就没那么好开了，所以还是劝君保管好。<br />
<br />
&nbsp; &nbsp; 干部身份：<br />
<br />
&nbsp; &nbsp; 在中国社会体系中，公民分三中身份：农民、工人、干部。农民归农业部管理，工人归劳动局管理，而干部归人事局管理。大学生属于国家培养的专业人才，属于国家干部身份。很多农村来的朋友家里都希望孩子通过念大学而改变祖辈农民的身份（这里没有贬低农民朋友的意思），而很多人却从毕业后就莫名其妙的丢了自己的干部身份。当然也有很多是根本不在乎自己的干部身份。认为有就有，没有就没有，没有的话我至少是四年大学毕业学士学位。其实你错了。如果你没有留住你的干部身份，可以说你的大学就是白念，而你只是个有学士学位的工人，根本没有改变你自己（就很多农村朋友来讲）。<br />
<br />
&nbsp; &nbsp; 干部身份靠什么来？靠三方协议，靠派遣证。派遣证就是你大学生干部身份的证明，假如某天你被提干时，这个就是你可以被提干的证明，因为你是干部身份。否则你就不能被提，因为你是工人。<br />
<br />
&nbsp; &nbsp; 也许很多人说：我不会被提干，那我要干部身份干嘛，我就想挣钱。<br />
<br />
&nbsp; &nbsp; 那么我现在要说的就是职称评定了。其中也涉及到关键一年的具体事宜。<br />
<br />
&nbsp; &nbsp; 大学生持报到证到单位上岗后，必须要经过一年的见习期（国家规定。相当于中专、大专的实习期）。见习期满后，本人必须记得要签“毕业生见习期考核鉴定表”，这是你转正的鉴定表（从此你就拿正常工资了），说明你已经是一个合格的人才了（见习期需要在同一单位完成，也就是你的三方、派遣证以及你的转正证明表，这三个上面盖的要是同一单位的章，否则视为无效）。紧接着要记得填写“国家统一分配大中专院校毕业生 专业技术职务任职资格认定表”，也就是初级职称评定表（这些事要你自己留心，没有人会提醒你去做的，表要到人事部网站下载）。具体可评定的职称可以到人事部网站上查询（职称最好和自己学的专业有联系，否则到中高级评定时比较难）。现在我国职称评定这块要通过考试（除艺术、工艺美术、体育教练员及广播电视播音4个系列外，申报其他初级系列都需要通过考试才能获得初级专业技术资格。考试采用闭卷笔答方式，实行统一大纲、统一命题、统一组织。申报人员参加北京市初级专业技术资格考试成绩合格，即可取得《北京市专业技术资格证书》），以考试来决定你是否能评上职称。当然有些职称评定也不排除某些单位内部的人为原因。在你初级职称评完四年后，也就是你工作的第五年，你可以申请评定中级职称。再五年后，也就是工作第十年，评定高级职称（具体可上网查）。这样你一辈子的职称就基本评定完了，所以有些幸运的人10年就可以当上“高级工程师”。在我国，无论各行各业都会有职称评定，而且相信任何单位都希望有那些有职称的人来工作，而且，就算你工作的单位没有职称评定一说，那想你如果是高级工程师，你拿的钱也绝对比别人多。而什么人能评定职称呢？有干部身份的人。所以从此看出，就算你不当官，你只挣钱，那干部身份对你来说还是有用的。<br />
<br />
&nbsp; &nbsp; 说到这里，基本上一般的情况都讲完了。但有些人会问了，我不是本地生，我户口不能留这，没公司给我签三方，你说的都是屁话。呵呵，那你就错了。为什么不变通的想想呢？例如：你在北京上学，但毕业后没有公司解决你的户口，怎么办？好办！在家里找公司签三方。因为你不能留京，所以你的户口会打回原籍，而档案与户口不能脱离，所以你的档案也要打回原籍，而如果你想保住干部身份（但想在北京工作），那你就托人也好，请客也好在家找个单位把你的三方签了。然后其他和上边一样，只是一年后记得回家去办转正与职称评定。如果嫌麻烦，没关系，你就回家就业，别担心回不来北京，因为如果你有职称，那么你就可以以后来京找工作，而你也会属于人才流动，这时只要你提出单位也会适当解决你的户口问题，否则凭你打工者的身份要求单位解决你户口，梦吧你！<br />
<br />
&nbsp; &nbsp; 三方协议的签署是没有限制的，与任何公司或单位签都可以，不一定要国企或事业单位，否则每年那么多大学生就业，还不80%都要丢了干部身份，国家还不成白痴了。但要保证一点，就算是三个人的小公司也能签，但不要在一年内倒闭，否则你就没有人给你签转正了，切记切记。<br />
&nbsp; &nbsp; 又有人说了，我想考研，我明年要考，我就不签三方了，随便找个活干干，挣点钱完了。那你就大错特错了。<br />
&nbsp; &nbsp; 记住，不论任何时候，别放弃你的身份！<br />
&nbsp; &nbsp; 如果你要考研，那么你属于在职考研一类，我们分类说一下。<br />
&nbsp; &nbsp; 假如你考上了：<br />
&nbsp; &nbsp; 你6月大学毕业，7月到单位报到。第二年7月，你转正，初级职称评定。研究生9月入学。当你研究生入学时，哥们已经是有身份的人了，3年研究生读完，哥们出来直接拿正常工资了（进私企），没有见习期一说了（研究生毕业也有3个月的见习期）。如果你进国企或事业单位，那么研究生3年算工龄（我不知道私企是不是，但国企或事业单位绝对是），你出来就可以评中级职称了，哥们又赚了一大笔。所以你说你该不该保你的干部身份？<br />
&nbsp; &nbsp; 假如你没考上：<br />
&nbsp; &nbsp; 没考上更简单，直接继续工作，回头等着评中级，没差什么。<br />
&nbsp; &nbsp; 很多人总是怕麻烦，其实一切就是这么简单（我打的不简单），如果你做了，那么也许我的话会改变你的一生，如果你没做，那么就当给我捧场，也谢谢你，我们又少了一个竞争对手。不过要记住一句话，别因小失大，谨记！ 　<br />
