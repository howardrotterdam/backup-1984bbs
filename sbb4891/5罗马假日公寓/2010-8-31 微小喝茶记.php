第二天联系X警官未果。第三天接上头，约好时间后问是否需知会家属今夜不归，X笑曰不怕不怕，问题不大。<br />
先给朋友打电话，只告诉他X的电话，约好4小时后再联系，否则就通知我GF今晚不回去了，告诉她我喝多在朋友家睡了。<br />
打车直奔目的地。进门报名找X，保安摁下遥控器，打开通往楼上的自动门。敲开办公室，迎面一张笑脸，X客气的把我让进屋里。简单相互介绍，X拿出一张传真件，铺开记录本，直奔主题。<br />
X：今年你向境外汇过款吗？<br />
我：昨天下午向香港汇过一笔，是公事，金额***<br />
X：不是这个，是100人民币，大概6月，收款人是方增福。<br />
我：冯正虎吧？上海，不是境外。<br />
X：对，是冯正虎。哦，不是境外。说说这事吧，这个冯是干嘛的，你和他是什么关系。<br />
我：冯是上海著名维权人士......<br />
X：你怎么知道他的？<br />
我：因为成田机场事件。成田机场是这么这么这么回事。我是从网络上知道的。<br />
X：政府为啥不让他回国？你在哪个网站看的什么文章？<br />
我：政府的理由没公开，据说和世博会相关，这个您应该比我懂。我是在壹报上看到冯正虎长篇访谈了解此人的。（本不想提壹报，闪念又认为这是壹报的荣耀，可能对不起翟明磊先生了）<br />
X：壹报网址是啥？<br />
我：记不住，可以google一下，您这应该没有上不去的网站吧。<br />
X：我这也不行。那个访谈号召大家捐赠了吗？<br />
我：印：罄捶胂壬发帖公布账号，接受捐赠。我认为冯的行为可以让我们的社会变得美好，他用温和、理性的方式帮助那些受伤害的人群维护他们应得的权利，同时缓解了日趋紧张的官民冲突。他拿出身家性命去推动这个社会的进步，而我只是出一点力，请他吃顿午饭而已。<br />
X：你是不是党员？<br />
我：（笑）不是。<br />
X：（笑）我猜你的思想挺活跃的吧。主张民主、人权这些东西。你生在中国这片土地，她哺育了你，你就得接受他的现状。社会进步要一点点来……（背诵人民日报）。<br />
我：（笑，不语）<br />
X：你是学法律的，不应该犯这种错误。<br />
我：这事和法律没啥关系。作为普通民众，我们不知道行为的边界在哪，后果是啥？法律本不禁止自然人之间的赠与行为。在10分钟前，我还不知道给冯捐款是被禁止的。<br />
X：社会上穷人多了，你能捐的过来吗？你这事说大不大，说小不小。你看（指传真件），这是省厅直接交代下来的，你的个人信息，捐款时间、金额很详细。你已经上了**名单知道不？<br />
我：（笑）这门槛也太低了吧。这样你们国宝能忙得过来吗？对了，我前几天出差香港，出入境时都被扣下核查信息，并且开箱检查，很难用抽查概率解释。回来后我去公安局出入境大厅要求解释，结果没理我。是不是就因为这事？<br />
X：有可能，这个我们基层机构不掌握。今天是我找你，说明事情不大，要是市局找你就麻烦了，要是省厅直接找你，估计你事就大了。<br />
我：我要是捐一万块钱是不就事大了？<br />
X：那当然，估计省厅直接找你了。你说你工作这么好，人还年轻，一定要珍惜，不要再参与这事了。我们本来可以直接到你单位找你谈话，到时肯定要求你的领导要在场，你说你以后怎么办？或者我们到你单位直接给戴上手铐，警车拉走，以后你还怎么工作？但你不用害怕，你的问题不大，所以我们秘密约谈，谁也不知道，但只有这一次机会，下次就没这么客气了。<br />
我：没经传讯手续也可以戴手铐拉走吗？<br />
X：到时就说怕你跑了呗。还拉着警笛，让所有人都知道。<br />
我：我这小角色还是不劳大驾了。您说基层机构忙，平常都忙啥？<br />
 X：咱这地方不太平，有很多重点人，他们当中很多现在都去美国了。<br />
我：咱这是出过几个人物，比如xxx、xxx和xxx。<br />
X：他们倒没啥。你是不是喜欢思考谈论民主、人权。我见过很多人都是这样，但这是在中国，我们国家的情况你也应该理解。本来我的任务是对你进行说服教育，你的学历高，我也不和你争论，你谈谈的看法吧。<br />
我：我也不想谈那些宏大的东西。我们国家1998年就签定了联合国人权公约，人民的政治权利也写入了宪法。我的阶段性愿望只是落实这些法律文件而已。甚至退后一步讲，只是希望能免于恐惧的生活，知道明确的自由边界。人可以诚实的生活，与人为善。你看你今天作为警察讯问我，态度友好，多少改变了我对警察一贯的认识。这就是一个小小的良好互动的开始，警民之间开始正常的沟通、交流，一点一点的改变一个人，一点一点推动社会的进步。<br />
X：我的女儿、女婿都是博士后，所以我对你们知识分子比较客气。换做别人讯问你未必这样。<br />
我：您讯问我时的身份是警察，良好态度的私人原因并不重要，重要的是一个警察正在用良好的态度与一个公民交流，您已经用行为推动这个社会变得美好一点点了。就像冯的行为在推动社会文明的进步，我愿意尽一份绵力，同样可以使我们的生活更美好一点。<br />
X：嗯，咱们就到这吧，我还有事。强调一遍，一定要珍惜前途，不要再参与这事了。下次再见就没这么客气了。<br />
我：（笑）那我回去上班了。<br />
事后出差，断断续续的利用睡前时间回忆记录上文，但可保证大体准确。上网搜一下，喝茶大多由于签署08宪章，因为这样的小事被喝茶的还没发现，故命名微小喝茶记。有几点感受可与84网友分享：<br />
1.他们唯一的手段就是制造恐惧。喝茶讯问，通知家属，通知单位属常规手段。采取强制措施，剥夺人身自由是高级手段。肉体消灭就是终极手段。他们并不试图说服你，可能工作经验表明洗脑投入的人力、时间成本太高，效果不明显，还有可能给对方发挥的机会进行反洗脑。于是他们直接制造各种恐惧，万不给你免于恐惧的自由。<br />
2.基层国宝组织业务素质偏低。他们似乎缺乏一种标准的工作流程，也没有可供执行的工作手册。讯问者只是本能的反感民主、人权这些词汇，并没有一种推敲过的标准反驳可以引用，你可以很容易的让他语塞。当然，基本上他们是拒绝交流的，他们的主要工作手段见第一项。<br />
