																											<div id="postmessage_380097" class="t_msgfont">我想我是比较有资格来提一些建议的。因为从小学开始到高中，我英文都是很烂的，高中时英文还有不及格。高考英文也就中等成绩。后来四六级也都是勉强过。不过后来一直没有撂下，一直到现在自认为水平还可以。<br />
<br />
我的建议如下<br />
1，专门背单词没有必要，除非要对付某些考试。单词应该通过不断的阅读来积累。而且有上下文帮助的记忆相较单独的记忆要有效得多。可以试试把每次阅读不认识的词记下来，然后在重点记忆。<br />
<br />
2，一开始不要尝试阅读名著之类的东西，因为里面的句子很长很拗口，初学者容易半途而废。<br />
<br />
3，阅读材料我建议直接看老外写的，而不要看国人自己写的，因为语感毕竟有差别。<br />
<br />
4，选择自己感兴趣的题材阅读。一开始反复阅读一些中短篇的文章，长篇的可以分成若干短篇来读，不必勉强一次读完。这里我强调一下反复阅读同一篇文章，这样可以帮助你更好的理解句子结构，短语的用法。因为在第二第三遍的时候，你已经知道内容是什么，就可以更加专心于语言本身。<br />
<br />
5，文本的难度我觉得类似时代周刊是一个比较合适水平，也就是普通美国人阅读的水平<br />
<br />
6，开着英文字幕看Friends，反复看。这应该是一个非常有效的办法，而且可以达到多重效果。练习听力，增加词汇，练习语感。或者任何你有兴趣的电影电视。记住开英文字幕<br />
<br />
7，不要用英汉词典，用英英词典。也不要逢生词就查，要学会猜词意，以及就着生词阅读，因为这会是以后的常态<br />
<br />
8，持之以恒，保持对英语的兴趣，如果没有兴趣，很难坚持</div>

							
							
							
							
