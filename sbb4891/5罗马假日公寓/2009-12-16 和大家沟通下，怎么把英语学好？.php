<br />
听歌、看美剧可以锻炼听力，个人认为是没有问题的，听的时候专注些，别把注意力全放在旋律或者画面上就可以了。不同的美剧因为题材不同，因此对扩充你能熟练运用的词汇帮助很大。<br />
