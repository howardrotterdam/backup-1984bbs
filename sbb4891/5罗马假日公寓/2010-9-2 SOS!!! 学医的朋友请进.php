													<h2>唉，你们都不了解应用中的中医</h2>
						
						
																											<div id="postmessage_812109" class="t_msgfont">中医绝不是不可靠，只是在关键时候不那么靠得住而已。不然怎么解释那么多中药、中成药还有乌七八糟的中药提取物注射液充斥在医院中。举几个小小的例子：<br />
1、病人得了小感冒，按道理说只要几毛钱就能打发，若是身强力壮的不吃药也就好了，但是这样的话医生的额外收入不就没有了？所以“银黄颗粒”、“感冒灵胶囊”，价格也不贵，十几块一盒，一般人也挺吃这一套。不要说这些东西的效果还挺不错，这并不奇怪，里面掺了退热药嘛。<br />
2、许多东西是在现代医学的理解之外的，特别是一些头脑糊涂，逻辑不清的病人，这样的人永远在病人中占大多数。“为什么最近老是上火？”，“为什么我的舌苔那么厚？”，有时候医生也不知道为什么，几乎内科学的一半都可以和上火套上关系，再说谁会有空闲的十多分钟时间向病人解释所谓的上火症状包含了包括感染、炎症、甚至自身免疫性疾病在内的一大类疾病？一般你所看到的情况就是医生连连点头打着哈哈，“对，对，你这是上火了，要注意休息啊。来，我给你开一些清火的中药。”<br />
3、许多疾病是很难治愈的，甚至控制疾病进展就是了不起的成就。比如肾病、类风湿还有常见的高血压、糖尿病等等。就拿肾病来说，“肾炎康复片”，“白令胶囊”之类的东西占着肾内科药物的半壁江山。医生说实话，病人不爱听，老百姓希望听到确切的答复，不喜欢专业术语的绕弯弯，比如“白令胶囊里面有冬虫夏草，可以调节免疫。”这样斩钉截铁却完全缺乏逻辑的话在效果上比正规的“用糖皮质激素治疗长期控制的可能性大约在60％，复发的风险长期存在。”这样的话不知要强多少。<br />
4、对于拒绝服用西药的病人，不消说这样的人还不少，陈晓旭就是其中一个。含有西药的中成药就是很好的推荐，珍菊降压片里含有“芦丁，氢氯噻嗪，盐酸可乐定，野菊花膏粉，珍珠层粉”，谁都知道里面起作用的成分是氢氯噻嗪和盐酸可乐定，还有在美国被禁的“消渴丸”，就是其中含有“葛根、地黄、黄芪、天花粉、玉米须、五味子、山药、格列本脲”，格列本脲虽然被排在最后，但是却是真正起作用的。当然这些药物的禁忌和不良反应都是尚不明确。我亲眼见过一个痛风的病人服用珍菊降压片，全然不顾氢氯噻嗪可能引起的肾损害。<br />
5、医保中心对药物费用和抗生素比例是有严格规定的，特别是公立医院，这种压力通过医院转到每一个医生头上。医院全然不顾实际情况死板地订出了所谓的“廉价药比例”和“抗生素费用比例”，超标者就要被扣去原本已少得可怜的几千块奖金。可是改用的药不能不用不是？自然有对策，多开中成药，把比例降下来就是了。<br />
6、坦率地说，医院里常用的中药尽管作用可疑，但副作用还是很小的。由于存在“安慰剂效应”，许多头晕、胸闷却长期查不出什么毛病的病人，许多都是心理因素引起的，这个时候用昂贵的中成药进行暗示疗法效果不知道会有多么好。<br />
――――――――――――――――――――――――――――――――――――――――――――――<br />
絮絮叨叨说了这么多，只是希望能够解决一些人为什么医生一点不相信中药，中药却满天飞的疑问，现在中药厂商、医务工作者、医药监管机构已经形成了巨大的利益同盟，其中的所有人都靠中药着分一杯羹。具体到楼主的问题，我认为一个非医学专业出身的普通人很难把病情描述清楚，这里的组员即使有医学知识，也不会有空帮你分析病情，他们早就义愤填膺的批判中医去了。所以靠谱的办法是找一个靠谱的妇科医生，带上病人，坦率的交谈会给你惊喜，医生毕竟是知识分子，大多数人心中多少还有一些“清高”、“良知”和“耻辱感”，若是有熟人介绍，那就更好了，一般的医生肯定会尽心的。</div>

							
							
							
							
