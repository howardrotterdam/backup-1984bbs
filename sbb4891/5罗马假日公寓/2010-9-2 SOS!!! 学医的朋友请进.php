																											<div id="postmessage_812581" class="t_msgfont">何去何从----读六六的《心术》<br />
<a href="http://www.ruanyifeng.com/blog/2010/09/where_i_am_going.html" target="_blank">http://www.ruanyifeng.com/blog/2010/09/where_i_am_going.html</a><br />
1.<br />
<br />
一位医生朋友推荐的，说这本小说值得一读。<br />
<br />
拿上手后，我发现它真的很吸引人。12万字的全文，我居然一口气就读完了。<br />
<br />
当然，我在这里说&quot;吸引人&quot;，不是指它有多高的文学性，也不是说它对现实有多么深刻的揭露和批判，而是指它是一个蛮好看的故事。<br />
<br />
不仅主人公的命运打动人心，而且读完后，确实会让你站在医生的角度，观察和思考这里的人生和社会。<br />
<br />
<br />
<br />
2.<br />
<br />
《心术》讲述了三个医学博士的故事。<br />
<br />
他们都是上海一家著名三甲医院&quot;神经外科&quot;的医生。（不知道&quot;神经外科&quot;的朋友，可以把它简单理解成，开刀切除脑瘤。）<br />
<br />
三个博士的年龄和资历都不同，唯一的共同点就是，内心深处，他们都希望成为一位好医生。但是，在残酷的现实面前，他们发现这是一个多么艰难、几乎不可能完成的目标。<br />
<br />
　　* 老大，主任医生刘曦，热爱工作，关心病人，但是六岁的女儿肾衰竭，急需换肾。他无法找到肾源，只能眼睁睁看着女儿病情一天天恶化。医生做得再好，却救不了自己的女儿，这是不是命运的嘲讽？<br />
<br />
　　* 老二，副主任医生霍思邈，出生于医生世家，学医更多是因为家庭传统，而不是本人愿望。虽然技术高超，但总觉得，这个职业不符合自己的选择。用他的话说，就是&quot;十年医生当下来，觉得自己像个木头一样，已经跟社会完全脱节了。&quot;<br />
<br />
　　* 老三，主治医生郑艾平，一个没钱没房没地位的小医生。经过四年本科、五年硕博，两年住院医生、两年主治医生之后，他依然没有机会看门诊，只能在手术室和急诊间里当无名英雄。因为病人不信任年轻医生，没人选择让他开刀。<br />
<br />
小说讲的就是这三个人的经历。他们怀着热忱，离开学校，来到这所全国著名的医院，可是等待他们的是什么呢？<br />
<br />
3.<br />
<br />
根据小说的描述，医生这个职业有三大现实。<br />
<br />
第一，长时间、高负荷的劳动。<br />
<br />
每天都有手术，有时一个人一天好几台。长的手术会持续4、5个小时，甚至更久。&quot;本周，我最长睡觉时间是周三，五个小时；最短睡觉时间是周一，就没睡，大夜班过后直接是手术，然后就地躺了半个钟头。&quot;&quot;这个医院的繁荣下面，垫的都是医生的骨灰。&quot;<br />
<br />
第二，收入不高，尤其是年轻医生。<br />
<br />
刚毕业的住院医生，一年收入四、五万元。即使工作10年之后，明的收入也不过&quot;基本工资2千多，奖金3千到4千&quot;。在上海这个地方，这只是温饱水平，根本不要提买房买车了。<br />
<br />
相比工资，医生的劳动价格更低。&quot;普通挂号费2元5角，医生个人可以拿到2毛。&quot;如果一天门诊看60个病人，也不过才12元。&quot;一台手术，从护工的搬运到护士的准备，到麻醉师的检测，到辅助医生的开颅缝合，到主刀大夫的取瘤，全程耗时最短三个钟头，长就说不好了。全部人工费多少？一千块以下，其中大头还要被医院拿走。&quot;当然，&quot;手术最后的结算价格，从三万到八万不等。其中，器材费远高于人工费。开刀是不值钱的。&quot;开刀医生的收入，要靠器材费提成弥补。<br />
<br />
第三，医患关系紧张，没有尊严。<br />
<br />
医生被打被骂，是常有的事。&quot;新医生值夜班，第一件要知道的事，就是如果被家属追打，如何逃脱。&quot;<br />
<br />
医生与病人不再是治病救人的关系，更像是互相提防的对手。写病史的时候，一个疏忽，或者有一项检查没做，都可能成为医生将来败诉的证据。至于手术，则更是只能成功，不能失败，否则病人家属就会认定这是医疗事故。所以，到了后来，只要病人本人不提出来，医生就不敢开刀，&quot;因为病死是他的责任，开刀死了，就成了我的责任。&quot;<br />
<br />
（声明：以上段落都是引自小说，我不是医生，不能保证真实。）<br />
<br />
4.<br />
<br />
现在，让我们站在主人公的角度，假设一下：<br />
<br />
你终于读完9年或10年的医学课程，拿到了博士学位。来到医院后，却发现自己的收入，低得只够维持温饱，但是工作却非常辛苦，而且还可能被病人家属打骂、起诉，万一败诉还有被吊销执业许可的风险。你会怎么想？<br />
<br />
你还会一腔热情地对待医生这个工作吗？你还会拒收红包和回购吗？你还会把&quot;救死扶伤&quot;当作医生的天职吗？<br />
<br />
这就是小说中三个主人公的困境。他们有热忱，想做好医生，可是在现实面前却处处碰壁。个人的理想和操守，生存的压力，家人的期待，职业的艰难，一切都是如此坚硬，到底应该何去何从？<br />
<br />
5.<br />
<br />
----继续坚持理想？在现实中很可能会穷死累死。<br />
<br />
----放弃理想，追求个人利益？那么当初何必选择当医生呢，选择更轻松更赚钱的职业，不是更好吗。<br />
<br />
你觉得应该怎么选择？<br />
<br />
小说中，神经外科主任有一段话，可以看作鼓励，也可以看作自我安慰：<br />
<br />
&quot;有些人当了几年学生就不愿意做了，我恭喜他们，在年轻的时候他们就发现这样一条艰苦的道路不适合他们，他们还来得及转行。<br />
<br />
有一些人当了几年医生不愿意干了，我祝福他们，不干医生，干医药代表也很好，收入比医生高得多，得偿所望。<br />
<br />
但我更珍惜我们留下的这个团队，珍惜你们。有些人天资可能比你们好，可他们没成为一名合格的医生。因为他们差的那点点东西，你们有----信念。一个有着坚定信念的人，才会在我们这里经历各种打击磨难而无怨无悔。&quot;<br />
<br />
6.<br />
<br />
看到这里，有的朋友可能会说：这篇小说完全站在医生角度，不够客观全面，没有考虑到病患的观点，而且里面的人物太过理想化，显得不真实。<br />
<br />
在我看来，这些都不重要。真正重要的是，我们要追问：<br />
<br />
----想当好医生的人，却当不成。这样的困境是谁造成的？<br />
<br />
一方面，普通人看不起病，觉得医疗费昂贵；另一方面，医疗资源十分紧张，医生感到收入偏低。这到底是怎么回事？到底是哪里出了问题？<br />
<br />
医患之间紧张的关系，并不是因为医生没有医德，也不是因为病人刁钻，而是因为这两个群体被逼成这样。<br />
<br />
说到底，是我们的制度出了问题。在这个制度里，你只能按照它的规则行事，不服从就要被淘汰。医生和病人都是这个制度的受害者，他们被逼得对立起来。<br />
<br />
7.<br />
<br />
这就是，我写这篇书评的根本目的，我就是想问：在制度的压迫下，个人应该怎么办？<br />
<br />
你有一个很美好的事业，你也热爱它，可是你偏偏遇上了一个非常差的制度，它不让你堂堂正正做人、光明正大地追求理想，你该怎么办？<br />
<br />
其实，答案大家都知道，只是承认起来有点伤感。你一个人不可能改变制度，最多就是心里挣扎一下，然后不得不接受它为你定下的规则。<br />
<br />
我忘了是谁说的：摆脱一个制度的有效方法，就是成为这个制度里的成功者。真是糟到不能再糟的一句实话。<br />
<br />
8.<br />
<br />
小说中，主任说：&quot;人活着要有两个主义。理想主义和乐观主义。如果没了这两个主义，人生会变得暗无天日，活得行尸走肉。&quot;<br />
<br />
可是，理想主义和乐观主义，明明都是精神胜利法啊。<br />
<br />
这就是小说《心术》。怎么当一个好医生？它也不知道答案，或者知道但是不愿意承认，那么就精神胜利一下吧，让三个主人公最后都在个人生活中如偿所愿。<br />
<br />
（完）</div>

							
							
							
							
