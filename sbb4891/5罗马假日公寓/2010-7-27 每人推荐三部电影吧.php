不同的社会制度中会催生出不同的意识形态，而在极权主义蔓延的社会，唯一的意识形态就是没有意识形态，所有的人都是统一标准的工具，感情归零、思想归零、一切个人化的东西都归零。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; ――题记<br />
 <br />
&nbsp; &nbsp;&nbsp;&nbsp;说到极权主义，似乎所有人都可以脱口而出――德国的纳粹、意大利的法西斯。可是我们目前所处的社会所追求的共产主义同样属于极权主义的范畴。极权主义（totalitarianism）这个词在西方学界就是德国纳粹主义、意大利法西斯主义以及苏维埃共产主义的统称。<br />
&nbsp; &nbsp;&nbsp; &nbsp;从字面意思不难理解，极权主义的实质就是某种权利聚集后的专制与独裁，也许它与古代的专制统治最大的区别就是用思想禁锢的手段代替了武力震慑。据此，可以用三个关键属性来诠释极权主义――统一的意识形态、统一的群众政党、统一的社会控制。<br />
&nbsp; &nbsp;&nbsp; &nbsp;目前我们所处的世界中有没有极权主义国家？定义在上面，大家不妨自己去做一下对号入座的工作，我不认为我可以用文字的形式解释我对现今社会极权主义的浅薄见解。因为，在极权主义国家中，一切都要受到控制，这其中言论自然是重中之重。<br />
&nbsp; &nbsp;&nbsp; &nbsp;社会问题、政治问题不方便谈，那么我们不妨看一看电影中的那些极权主义社会。在匪夷所思的镜头面前，也许一颗原本平平稳稳的心会像受到电击一般跳动频率猛然加速。原来，社会竟然可以这样；原来，未来竟然就是这样……<br />
