 <br />
&nbsp; &nbsp;&nbsp;&nbsp;这部投资两千万美元的科幻片给人留下的最深印象有两个，一个是克里斯蒂安・贝尔英俊孤傲的外表，另一个就是枪械、中国功夫、日本剑道三合为一的“开枪体操”。然而这部电影中所勾画出来的极权社会却似乎是最冰冷、最黑暗的元素，值得人们思考。<br />
&nbsp; &nbsp;&nbsp; &nbsp;影片讲述了在第三次世界大战之后，核武器的使用险些摧毁了整个地球，存活下来的人们组成了一个统一的极权社会。在这个社会中的人没有思想、没有感情，连最基本的喜怒哀乐都没有，所有人都只有一张麻木不仁的脸孔。于是，所有与思想有关的艺术品都被统统毁掉了，你不能听音乐、不能看书、不能欣赏绘画，总之你的情绪不能够有任何的波动。为了达成这一目的，政府还推行一种情感抑制剂，从而让所有人失去了最基本的人类情感，变成彻底的行尸走肉。<br />
&nbsp; &nbsp;&nbsp;&nbsp;毫无疑问，这部科幻片中对于极权主义的勾画非常极端，对于社会中的人来讲，他们已经失去了做人的基本属性，变成了由政府集体放养的牲畜。可是这种禁锢实质上是虚假的，人性的强韧决定了人的体内必然需要有属于人的情感与情绪，而人的情绪则是极权主义最想要把持的核心要义。两者形成了最鲜明的对抗。<br />
 <br />
<br />
4、<br />
&nbsp; &nbsp;&nbsp; &nbsp; <br />
<br />
[北逃].Crossing<br />
 <br />
导演：金泰均 Tae-gyun Kim<br />
 <br />
&nbsp; &nbsp;&nbsp; &nbsp;对于朝鲜这个中国的近邻，似乎有太多被妖魔化的成分夹杂其中。苏联解体后，朝鲜一度视中国为世界社会主义阵营的老大哥，可是随着中国的“变质”，朝鲜这个国家越来越自闭起来。他们恪守着自己对于社会主义的梦想，将自己牢牢地锁了起来。<br />
&nbsp; &nbsp;&nbsp;&nbsp;2009年2月刊的美国国家地理杂志的封面就是《北韩大叛逃》，通过新闻通讯的形式记载了朝鲜国民冒着死亡的危险逃出过境，渴望得到重生的苦苦追寻。可以说，这部《北逃》与德国电影《窃听风暴》有着诸多的相似之处，虽然内容上完全没有同工之处，可是在灵魂上总让我感觉有某种契合。<br />
&nbsp; &nbsp;&nbsp; &nbsp;这部出产于韩国的电影选择的是个非常敏感的话题，使用的却是亲情这一温暖的手法。即便如此，这样一部斥责社会主义的电影还是毫无疑问的被国内封杀了。“封杀”是我非常讨厌的一个词，因为这象征着一定程度上的言论不自由，而言论不自由的社会总像是蒙着薄雾的玻璃，朦朦胧胧的看不清外面的世界，必须要道听途说才行。如果有一天，“XXX惨遭封杀”“XXX永久的退出XX舞台”等字眼不再出现在一个国家的媒体中，那么这样的国家才算是真正的民主，这个国家的人民才算具有真正的人权。<br />
 <br />
<br />
5、<br />
&nbsp; &nbsp;&nbsp; &nbsp; <br />
<br />
 <br />
<br />
[窃听风暴].The Lives of Others<br />
 <br />
导演：弗洛里安・亨克尔・冯・多纳斯马 Florian Henckel-Donnersmarck<br />
编剧：弗洛里安・亨克尔・冯・多纳斯马 Florian Henckel-Donnersmarck<br />
 <br />
&nbsp; &nbsp;&nbsp;&nbsp;一战结束以后，德国的悲剧便诞生了，各种割地赔款使得这个曾经自誉为天之骄子的国家变得一片狼藉。在这种情形下，希特勒与他的纳粹主义得以滋生蔓延，于是爆发了二战。二战结束后，德国的悲剧又诞生了，在分崩离析的国土上从此竖起了一道柏林墙，一个国家一分为二成为两种体制的两个国家。这种情形与当年苏联与美国携手划出“三八线”，割裂出南北朝鲜的境况出奇的相似。唯一不同的是，德国是战争发起国，而朝鲜则是完完全全的战争受害国。<br />
&nbsp; &nbsp; 《窃听风暴》的故事发生在东德，讲述了这个极权国家通过秘密警察的形式对全体国民进行监控的故事。当初的东德国安部雇佣了9万特工18万线人对全国上下进行了地毯式的监控。在柏林墙竖起后的30年间，平均每天有8人以“破坏国家安全”的罪名被逮捕；两德统一后，这些监控资料被公开，国民可以自由查阅，这些资料如果一本一本的铺开，足有1000公里长。人们可以从资料中查到关于自己的信息：朋友、亲人、同事之间的相互出卖。这无疑是一具潘多拉的魔盒，释放出的是无穷无尽的恶毒与凄厉。有的夫妻双双自杀，原因就是资料中有他们互相出卖的记录。正如这部电影中得瑞曼自寻短见的女友。<br />
&nbsp; &nbsp;&nbsp;&nbsp;当看到这部电影的最后，那句&quot;谨以此书献给HGW XX/7&quot;出现的时候，我由衷的为魏斯勒感到高兴，他选择了正确的道路。可是其他的绝大部分执迷不悟的人们呢？他们盲目效忠的党国，竟然缔造出了如此之大的惨剧，更可悲的是他们就像被施以魔法的小丑，只有执行的力气而全无思辨的能力。这是极权主义制造的悲剧。<br />
 <br />
<br />
6、<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;<br />
<br />
[大逃杀].Battle Royale<br />
 <br />
导 演： 深作欣二 Kinji Fukasaku<br />
&nbsp;&nbsp;编 剧： 深作健太 Kenta Fukasaku<br />
 <br />
&nbsp; &nbsp; 《大逃杀》里的极权实质上包含的更多是类似于古代的“专制主义”，即通过可怖的军权实现对于全体国民或者某一国民群体的镇压。在这部电影中，受到镇压的是未成年人。<br />
&nbsp; &nbsp;&nbsp;&nbsp;故事讲述的是经济大萧条时的日本，国家处于崩溃的边缘，而此时国内的未成年人则更加癫狂的破坏这个生命垂危的国家。于是一项被命名为BR的教育法规应运而生：规定每年选取一个初中三年级的毕业班级，将所有学生置于一个荒岛上，三天内相互厮杀，最后剩下的一名幸存者为赢家，得到生存的机会。这场玩命的游戏在国内的媒体上会得到了全方位的报道，以起到杀鸡儆猴的效力，让全国上下的未成年人变得安分一些。<br />
&nbsp; &nbsp;&nbsp;&nbsp;如果只是一场单纯的杀人游戏，《大逃杀》绝对不会受到这样的追捧，其内里所要弘扬的是生存的考验中，人性的秉持与沦丧。从一个大环境中来说，这就是对于一个人在极权主义社会里的人性考验，也就像《银翼杀手》里的人性考试一样。于是，在这样的环境中，每个人的阴暗面与阳光面得到了淋漓尽致的展现，不需要再伪装、在虚假，所有人都必须用自己的残忍的一面来面对所有人。谎言、猜疑、出卖，所有的可悲统统袭来，剖析出了丑陋的人性。<br />
 <br />
<br />
7、 <br />
&nbsp; &nbsp; <br />
<br />
[日本锁国].Vexille<br />
 <br />
&nbsp;&nbsp;导演：曾利文彦 Fumihiko Sori<br />
编剧：半田遥 Haruka Handa<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;曾利文彦 Fumihiko Sori<br />
 <br />
&nbsp; &nbsp;&nbsp;&nbsp;纳粹与法西斯的极权在很大程度上是外扩的，他们寄希望于用侵略完成天下大一统的局面，建立庞大的帝国极权社会。而共产主义的极权有很大程度上属于内敛的，他们寄希望于在一个特定的地区内实现自己“各尽其能、各取所需”的理想化社会。当年欧文等人模拟建成的“乌托邦”微型社会就是这样的形式。因此，极权的国家形态便有两个相反的出路，一条是外扩，一条是锁国。<br />
&nbsp; &nbsp; 《日本锁国》是一部科幻动画电影，但是我认为在这部电影中极权社会从某种程度上反射了锁国思路的极权主义。当然，这种锁国是消极的，是外表光鲜却内里苦闷的。例如现在的朝鲜，基本上属于锁国的状态，他们不断地通过军备竞赛来向世界展示自身日子增强的军力，可是另一方面在不为人知的国家内部，却是连年的饥荒。<br />
&nbsp; &nbsp;&nbsp; &nbsp;全球化趋势越来越明显的今天，大同世界尚只是一个梦想的今天，锁国意味着给自己堵上了所有出路，将自己逼上了绝路，是一种类似于自杀的行为。东西德的合并昭示了南北韩未来的发展方向，固步自封的社会是难以存活在这个世界上的。<br />
 <br />
<br />
8、<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;<br />
<br />
[末代独裁].The Last King Of Scotland<br />
 <br />
&nbsp; &nbsp; 导演：凯文・麦克唐纳 Kevin Macdonald<br />
编剧：杰里米・布洛克 Jeremy Brock<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;皮特・摩根 Peter Morgan<br />
 <br />
&nbsp; &nbsp;&nbsp;&nbsp;这部电影实质上讲述的是一个极权主义国家的形成与瓦解，更确切说是军权国家的形成与瓦解。同样是一部根据史实改编成的电影，《末代独裁》并非像其他的人物传记电影那样不瘟不火、中规中矩，无论从形式上还是演员的表演上，都颇具戏剧效果。尤其是弗里斯特・惠特克精彩绝伦的演出更是为这部电影增色不少。<br />
&nbsp; &nbsp;&nbsp; &nbsp;独裁社会的最大弊病在于国民个体化的磨灭，所有的人必须忠诚于国家与领袖，而且不能够有所谓的个人见地。在这样的情形下，即便是国家的方向是错的，你也必须要完完全全的遵守，不能够唱反调。如果一个国家之中没有敢于在政治上唱反调的人出现，那么这个国家的政治就是不完整的，也是片面的，至少它代表的并不是全体国民的意识，甚至连大部分人的意识都算不上。更严重的就是像电影中所讲到的，只代表了领导人自己的意志，这是最纯粹的个人独裁，比政党独裁似乎要更独一些。<br />
 <br />
<br />
9、<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;<br />
<br />
[浪潮].Die Welle<br />
 <br />
导演：丹尼斯・甘塞尔 Dennis Gansel<br />
编剧：丹尼斯・甘塞尔 Dennis Gansel<br />
 <br />
&nbsp; &nbsp;&nbsp; &nbsp;我很喜欢这部电影的原因是，它就像一个模型或者说一个实验，用最直观最形象的手法展示了极权主义的诞生。本片根据德国小说家托德・斯特拉瑟的同名作品改编而成，讲述了一个为期一周的“独裁政治”活动课。这个活动周在教师的悉心策划下，制造出了一个向极权主义过渡的学生团体――“浪潮”，他们创造出了自己的标志、口号、宗旨甚至是章程，就宛如一股浪潮一般席卷了整座城市。<br />
&nbsp; &nbsp;&nbsp; &nbsp;值得一提的是，这部电影中所讲述的故事来源于真实事件。1967年4月，美国加州Palo Alto Cubberley高中的一位历史教师Ron Jones在讲授关于纳粹德国的内容时被学生的问题问倒：“为什么德国人声称，对于屠杀犹太人不知情？为什么无论农民、银行雇员、教师还是医生都声称，他们并不知道集中营里发生的惨剧？”下课后，他仍不断思考着这个问题。为让学生切实理解法西斯主义，他开始了一项大胆的试验――成立“第三浪潮（The Third Wave）”。这就是电影《浪潮》中“浪潮”组织的原型，而他的学生们就是“第三浪潮”的成员。同样，他要求学生们说话前必须叫他“Jones先生”，设计了组织内统一的手势，并且以各种形式向学生们强调纪律与团结的重要性。很快，学生们都成为“第三浪潮”的忠实成员，并坚定地以Jones老师为组织的领袖。第三天，Jones暗中委派3名学生作为“督察”，向他报告那些不遵守“第三浪潮”规定的人以及组织的批评者和反对者；但是接下来竟有20余人毫无保留地向他告发自己的朋友（只是因为他们说了关于“第三浪潮”的笑话），甚至是对“第三浪潮”表示怀疑的父母。这时，Jones感到了事态的严重失控。于是第四天，以组织将在全国范围内被正式宣布成立的名义，他命令学生们次日到礼堂集合。终于在第五天，他在礼堂内设法使学生们从狂热的“第三浪潮”中醒悟过来，而学生们也最终真正明白了法西斯主义的可怕力量。而这期间，参加“第三浪潮”的学生人数已由30人（一说20人）左右增至200余人，其中后加入者大都逃掉了自己所本应参加的课程。<br />
&nbsp; &nbsp;&nbsp;&nbsp;这部电影充分体现了以煽动为主要形式的法西斯极权主义的形成机理，即通过抓住你的某种需求，并将其放大，再冠以一个看似正义的帽子，从而形成对个体的群体化的管理与操纵。<br />
 <br />
 <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; 结语<br />
 <br />
&nbsp; &nbsp;&nbsp; &nbsp;极权主义距离我们并不遥远，甚至此时此刻，我都也能看得到、摸得着。身为一个独立的人，必须要有自己独立的意识，这样才能最大程度的保护自己不沦为被人操纵与利用的对象。<br />
&nbsp; &nbsp;&nbsp; &nbsp;举个最简单的例子，我们随处可见的“传销”实质上就是一种微型的极权主义：首先进行洗脑，让你在拜金主义的驱使下变得惟命是从；其次发展下线，让你近乎狂热的拉自己的亲友入伙，并且排除异己；最根本的就是自下而上的绝对服从，你连自己组织的管理者是谁都不用知道，只需要知道自己所需要做的具体工作即可，在煽动性言论的怂恿下，一具行尸走肉便诞生了。<br />
&nbsp; &nbsp;&nbsp; &nbsp;认清极权主义的本质，才能够在极权主义到来时有自己的应对之道，才能使自己在乱世里保留住一颗冷静的头脑。</div>

							
							
							
							
