&nbsp; &nbsp;&nbsp;&nbsp;改编自英国作家乔治・奥威尔的同名小说，小说写于1948年，出版后随即在世界范围内引起了轩然大波，被认为是“反乌托邦”的旗帜作品，1956年便有同名电影上映，但是因为对原作做了大量修改而失去了原著的精髓，非常失败。1984年，即小说的名字所写到的年份，这部小说再一次被搬上银幕，并且成为了一部不朽的佳作。<br />
&nbsp; &nbsp; 《1984》虚构出了1984年的世界中只剩下了三个超级大国――大洋国、欧亚国与东亚国，三国鼎立，连年征战。主角所在的大洋国是个共产主义极权国家，所有的生活所需都是配给制，而作为国家的一员，所需要做的就是每天像工具一样的运转工作以及对于领袖“老大哥”的绝对崇拜。这部电影基本上秉承了原著中的主线，对共产主义社会进行了有效的反讽，从根本上否决了共产主义就是乌托邦的美好童话。这种社会中的所有人都像是上了发条的机器，没有灵魂、没有自由、没有个人意识，统一的蓝色制服下是一颗颗冰冷无比的心。这让我想起了曾经看过的一篇新闻报道，是一个美国记者描绘的他眼中的中国，标题大概意思是“蓝蚂蚁的国度”（蓝色指解放前与文革时期中国盛行的服装，蓝色列宁装、解放装；蚂蚁有两层意思，一为中国人口多，二为中国人就像蚂蚁一样的成群结队的干着体力活）。<br />
