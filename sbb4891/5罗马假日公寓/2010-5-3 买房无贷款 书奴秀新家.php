													<h2>秦老爹的书事</h2>
						
						
																											<div id="postmessage_599541" class="t_msgfont">“秦老爹”――秦晖，秦教授是也。我小时有一个动画片叫《蓝精灵》，那里面有一个智慧型的人物叫“蓝爸爸”，我就把秦晖叫“秦爸爸、秦爹爹、秦老爹”。关于“秦老爹”――秦晖与书的故事可以讲很多。 <br />
<br />
坐拥书城 <br />
<br />
父母曾说过，他们成家时唯一的“资产”是两人合在一起的14大纸箱的书。1982年他们回广西结婚，沿途最重要的一件事就是去古旧书店搜罗买书。从现在他们保留下来的黑白照片中还可以看到，几乎所到之处留影旁边都有一捆书。妈妈说，他们在上海约好了去照一张当时流行的上色的婚纱彩照，因为秦老爹在上海图书馆看书太投入，忘了这茬子事，害得妈妈做好了头、化了装，孤坐在照相馆里一直等到下班都不见秦老爹的身影，当时心想连结婚这样的事秦晖都能忘了，以后他还有什么不会忘，这婚不结也罢，让他和书结婚吧。 <br />
到了陕西师范大学，学校分给他们一间宿舍，书除了堆在地上、放在桌上、就是码放在床上，所以他们的第一件家具是花33元买了一个书架。但这仍然解决不了放书的问题，我姥姥就用两张单人床板给他们做了两个2米高的书架，算是给我妈的嫁妆。因为书增加的速度太快，很快家中就四壁皆书。我出生以后因为半边床上堆放着书，秦老爹无处住，只好借住在学生宿舍了。 <br />
因为秦老爹翻看的书常常不放回原位，家里的地方又小，一摞摞的书只好摆在地下，经常半夜就会听到哗啦一声地震般的声音，我们都习以为常地说，“雷峰塔倒了！”秦老爹有随手记卡片的习惯，经常是随便抓住什么纸头就写下一些想法，家里的一些废纸烂屑上可能都留有他的“灵感”，于是他有个规定：片纸不丢！不能打扫他的桌子，谁若胆敢清理他桌上的东西，势必迎来一顿咆哮。 <br />
<br />
这样的结果是家里很快就成了废品收购站，不但一捆捆、一袋袋、一摞摞的书籍胡乱摆放，一片片、一张张的烂纸头随处灰扑扑地散落着。很多在我看来毫无收藏价值的应景杂志和书籍完全可以淘汰掉，但秦老爹的座右铭是：“书到用处方恨少”，你怎么知道以后会用到什么书。 <br />
<br />
因为只进不出的结果，以及他们又从上一代人那里继承了一批书籍，有限的空间很快就只能向上发展，房间里已经看到不到一寸白墙，阳台上、厕所里、厨房里的顶柜每一个边边角角都利用起来。结果是，家里的书越多越找不着，他们常说人生1/2的时间都用来找书了，有时候写文章写到一半，要查找什么资料，在家里大索貌阅上上下下翻了个掉底儿，两手灰灰愣是找不着，于是大发牢骚说以后一定要像组织部进人一样严格把关限制买书，家里有书找不着还不如去图书馆借书来得方便。 <br />
<br />
话是这样说，可是遇到自己心仪的书，一定又会急逅逅地赶快收入囊中。现在家中到底有几万册书，谁也没有统计过，只是知道1998年我们搬家，搬家公司光书就拉了几趟，每次把载重汽车的底盘都压得超过了底线。2000年搬家，300个纸箱子来回使用了四趟还不够用。后来有记者来过我们家，以《秦晖坐拥书城》为题写了一篇文章。电脑时代他们的购书速度有所减缓，但是别人赠书的数目仍不在少数。家中的地砖因为受重不均匀，已经左一块右一块的松动起翘了，我们在家中走动就像踩石头过河，既要绕着书堆又要避开坏地砖，颇需要点技巧才行。 <br />
<br />
买书如痴 <br />
<br />
在他们的工资都不高的时候，买书是一笔不小的开支，有时也不得不忍痛割爱。 <br />
<br />
秦老爹至今念念不忘的是，1979年他在杭州清泰街的古旧书店，看到一本日文的仁井田著的《支那身份法史》爱不释手。但是7元的标价超出了心中预算，他在书店里看了很久，犹豫再三最后一步三回头恋恋不舍地放下了那本书，现在一想起来就后悔得要命。1980年我妈妈做毕业论文搜集资料时，在天津劝业场的一家古旧书店里结识了一位专门经营外文书的老店员。老人曾向她推荐了大量文革中抄家没收的外文书，也是因为囊中羞涩，与很多心仪的书籍失之交臂，现在每每想起还大呼可惜。在西安时，骑自行车去西安南院门的古旧书店是秦老爹的“经常节目”。有一次他碰到一本到处寻找的书，但偏偏所带的钱又不够，又怕第二天来已被别人挑走，只好把工作证抵押在书店带着书回来，第二天再去送钱，回来的时候又不忘再稍上几本书。 <br />
<br />
他们买的旧书中，有不少是流落在外的名人藏书，有他们的题记、眉批、签名，比如吴晗、陈翰笙、钱俊瑞等。以博学著称的史学家马雍去世后，秦老爹在旧书店买到了他的一些藏书，其中包括苏联出的两巨册《古希腊语俄语大辞典》，上面有马先生做的许多注记。马先生是搞中国史的，却对古希腊语如此有兴趣，这成了秦老爹教我要博览群书的一个例证。马先生的儿子也是学历史的，跟秦老爹还认识，有次见面秦老爹提到令尊的某某书如今在我这里，这位子承父业却把父亲藏书卖了的儿子“好像有点尴尬”。 <br />
<br />
1994年以前我们家住西安时，学校经常调整住房，我父母所任课的84、85、88级的学生都为他们搬过书，以至于现在这些已经成为博导、硕导的学生聚在一起时还说，当时他们一个班的男生浩浩荡荡的拉车扛书为老师搬家，很多人还以为给图书馆或系资料室搬家呢。 <br />
<br />
我是“看书科”生物 <br />
<br />
秦老爹眼睛不好，从小练就两个本领，一是站着看书。他小时候在南宁新华书店看书，一站就是一天，因为经常去，书店里的人都认识他，所以经常可以先把书拿回家，下次去再付钱。第二个本领就是看书时高度集中，可以屏蔽掉一切无用的信号和活动。这个习惯保持至今，比如说他看书时一般不喝水不上厕所，捧着本城砖般厚的百科全书杵在那里，一站就是个把钟头，除了接电话，当面询问只会得到没有有效反应的“唔、唔”之类的敷衍之声，反应极其迟钝。我和妈妈说他，他反而理直气壮地说“我是‘看书科’生物！” <br />
<br />
他在图书馆看书更是注意力集中到匪夷所思的地步，当年陕西师大的人都知道秦晖被锁到图书馆、资料室的事情。听妈妈说他在校图书馆书库的某个角落里看书，闭馆的铃声响了，大家都离开了，灯都熄灭了，他毫无察觉，等到光线无法再看书的时候，他才发现自己已被反锁在书库了，于是大喊大叫，被图书馆的人批评教育一顿后才被放出来。在历史系资料室也是，管资料的老师说：“下班了、下班了”，清场以后锁门下班回家。那时候电话还不普及，等到他知晓被锁在里面敲门高呼时，被其他老师发现，只好央求人家先去回家找金雁，然后再让我妈去找资料室的老师来开门。等到满校园里找人把他“解救”出来时，大家都调侃他说，索性你就在资料室里住一个晚上看个够，省得这么兴师动众麻烦诸多人。他信以为真地说，我倒真有此想法，主要是怕一夜未归家里人着急。 <br />
<br />
到北京以后，夏天他经常到北图去看书，一待就是一整天，往往到下班的时候，他会茫然地说，我记得刚进去一会儿，连中午饭都没吃，怎么就下班了呢？时隔多年，父母当年的同事到家里来做客，谈及秦老爹的这些往事，哈哈大笑之后总不免感慨，现在再无这样看书成痴的人了。 <br />
<br />
1978年文革后的第一批研究生人数不多，当时算是天之骄子，作毕业论文时可以在全国到处查资料，所以“泡”各地的图书馆是秦老爹的一大强项。他的导师赵俪生先生资历老学问又好，在学界到处都是熟人，而当时看书的人又少，秦老爹就凭着导师的一纸推荐信，在上海、杭州、南京等地的图书馆里享受到“本馆工作人员”的待遇。当时上海图书馆的馆长顾廷龙老先生跟行政方面打了个招呼，秦晖便住在图书馆的招待所，在图书馆食堂吃饭，可以入善本库看书。在杭州也是由于善本部主任张良诚先生的关照，秦老爹在孤山上的浙江省图书馆善本部（当时设在西湖中的孤山文澜阁）一待就是两个月。那年杭州的冬天奇冷，南方又没有暖气，他每天从杭大招待所穿过白堤上孤山，西湖上刺骨的寒风宛如刀割，孤山上室内也冻得捏不住笔。离开杭州时，他带走的除了抄录的上千张卡片，还有满手满脸的冻疮。三十年后，当我们一家在艳阳高照的夏日泛舟西湖时，秦老爹在波光潋滟的水面上仍感喟道：“杭州的冬天可真是冷啊”。 <br />
<br />
1979年在北图善本室里查阅资料，因为线装书纸张都很脆弱又吸水，容易玷污书籍，故而善本室里素有不能用钢笔的规定。当时善本室的一位老馆员，据说是“京师图书馆”时就入馆的，赵万里都很尊重他，此人惜书如命。秦老爹有一次不小心把一小滴墨水落在书边，使得负责任的老馆员大为光火，当即把他赶出善本室，后来写了检讨诚恳道歉赔了罚款才算了事。 <br />
<br />
曾有记者问过秦晖，有什么业余爱好，他回答“看书”。记者以为他没听明白又一次强调：“我说的是‘业余’爱好。”秦老爹说，我说的就是“业余爱好”。因为在他看来，其实文字工作没有专业与业余之分，他的工作就是兴趣所致。我上学期间总是为没完没了的升学考试所迫，书山所苦，免不了点灯熬油，通宵奋战，而他从来就对我那种苦行僧般的努力不以为然。他经常说，“我从来不相信头悬梁、锥刺股能够有什么出息，看书看到昏昏欲睡要拿锥子扎，能看进去什么？兴趣才是最大的动力”。当然，对于埋首厚厚的作业堆中分身乏术，困倦已极还要应付考试的我来说，说这些话的人完全是“白天不懂夜的黑，站着说话不腰疼”。 <br />
<br />
知识达人 <br />
<br />
当然，必须承认秦老爹看书的范围十分广泛，可以说只要和知识沾边的他都有兴趣。历史门类里中国史、世界史不算，与此沾边的考古、古文字、古生物、宗教、地理、自然、水利、兵器……他无一不感兴趣，范围之广恐怕许多以博学著称的专家都难与之匹敌。 <br />
<br />
他还尤其喜欢看地图，他自称小时3、4岁还不太认字的时候就迷上了看地图。那本旧地图汉字是从右排的，于是他把“黎巴嫩”读作“嫩巴黎”，很奇怪巴黎还有老嫩之分；竖排的“立陶宛”，他还认不准“陶”字，但是知道是“陶瓷”中的一个字，就叫“立瓷碗”。 <br />
<br />
文革大武斗的时候，他在宁波外婆家避难。那时中国什么都保密，沿海城市大都没有公开发行地图，他就自己跑遍了宁波的每条街巷，自己手绘了一张宁波地图，上面标注了他沿街看到的许多机关工厂。结果我爷爷见了大惊：在那个“敌情观念”浓厚的年月，他害怕会被人指为特务，把这张珍贵的手绘版地图付之一炬。还是个孩子的秦爹爹手捧地图碎片嚎啕大哭。这件事显然对他打击极大，以至于多年后说起都耿耿于怀。 <br />
<br />
这个童子功的练就还是非常了得，每每说起某个地方，他脑子里都会呈现出一幅“活地图”。当年在兰州大学和气象专业的研究生住在一起，他比那些当了多年气象填图员的人更加熟知中国的两千个县市，因此震倒了一票人。至于河流的流经区域、山脉的走向就更不在话下。往往别人一说起是哪里人，他就会如数家珍一般告诉人家，你们家旁边有什么河、有什么山。甚至在巴黎，他与法国朋友走在某条街上就会告诉人家这条街大革命前叫什么名字，1871年叫什么名字。因为他在当知青下乡时，就把《1871年公社史》所附的“巴黎街垒战”地图看熟了。 <br />
<br />
他搜集地图达到痴迷的地步，每个国家、城市、地区的地图，甭管是新的旧的他都一样喜欢。他认为，新的有新的用处，旧的有旧的用处，如果能配套逐年搜集全了就更有价值。在美国的每个城市以及汽车club里可以提供免费地图，他从美国回来如获至宝地运回来整整一箱子地图。我们在国外旅游每次他都比导游更了解当地的天文地理，还时常纠正人家的错误，搞得导游好没面子。很多与他一同出行过的人都说，秦晖是中国Number One的导游，不但可以讲解世界上任何一个地方的天文地理，而且是集全陪与地陪为一体的、可以穿越时空相互比较的知识性导游。 <br />
<br />
秦老爹还有一个习惯，喜欢用地名和人名的谐音作为日常词汇。比如热极了他就说“蒙哥大汗”；吃饱了他说“塞鼓肚儿”（塞古杜尔，几内亚的开国总统)；晚上困了，就说要去“躺铺儿”（坦普尔，英国坎特伯雷大主教）；去厕所小便叫“慢撒尿”（曼萨尼奥，墨西哥的海港）；蹲厕所叫“拉各斯”（尼日利亚前首都）；如果两个人正好“都拉斯”（阿尔巴尼亚的海港城市）呢？那只好“轮蹲”（伦敦）了；在里边看书不出来，等着“轮蹲”的人抱怨了，他就说“拉斯帕尔马斯（怕尔骂死，加那利群岛城市）”；平时说哪个人没出息，就说是“瓜拉丁家奴”（马来西亚一省会）；裤子松了，说“涅菲尔提提”（古埃及的王后）等等，不一而足。 <br />
<br />
与现在年轻人喜欢K歌一样，秦老爹也很喜欢哼歌。但他会唱的歌卡拉OK上一首也没有，因为他经常哼哼的三类歌曲都不是流行歌曲。他第一喜欢唱国歌，粗粗统计他大概能唱出50~60个国家的国歌；第二喜欢宗教歌曲，如《圣母颂》、《大开城门》等；第三喜欢特定历史事件中的歌曲，比如《一块牛排出卖巴黎》、《觉醒吧罗马尼亚》、《团结工会之歌》，甚至还有《党卫军之歌》，以及文革中的歌曲，像什么《知青之歌》和武斗中的《完蛋就完蛋》、《四二二战歌》之类，现在几乎无人知晓的带有时代烙印的歌曲。 <br />
<br />
与对知识的痴迷相反，热播的情感剧、青春偶像剧、反特剧，他从来不屑一顾，并感到大惑不解：现实中的人们总是忙忙碌碌，而电视中人物怎么有那么多的时间默默唧唧，无病呻吟呢？他的解决之道倒也简单：让那些痴男怨女们都去写论文，就会天下无事。</div>

							
							
							
							
