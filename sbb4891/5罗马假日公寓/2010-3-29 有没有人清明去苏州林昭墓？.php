　　从苏州回来，辗转宁波、海口，买房、装修，一路忙到十月底；再回北京处理家事，之后定居海口，数不清做不完的俗务。还没顾上写下只言片语，眼看清明又到。<br />
<br />
　　下面，以照片为据，给今年有意祭扫林昭墓的朋友作个向导。<br />
<br />
<br />
　　从苏州火车站算起，向西南方向约16公里到达木渎镇，灵岩山是木渎镇的一个大旅游景点。沿灵岩山景点大门向西的穹息路（230省道）行1公里多，可见路北侧的韩世忠墓园。<br />
<br />
<img src="http://upload.17u.com/uploadfile/2007/04/03/6/2007040322451583303.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
图：穹息路韩世忠墓园<br />
<br />
　　就在韩世忠墓园西墙外，有一个不显眼的路口，向北深入几十米就是安息公墓大门。<br />
<br />
<img src="http://upload.17u.com/uploadfile/2007/04/03/6/2007040322451516824.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
图：路口<br />
<br />
<img src="http://upload.17u.com/uploadfile/2007/04/03/6/2007040322451578456.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
图：安息公墓大门<br />
<br />
　　一进安息公墓大门，有左右两条上山的小路，右侧山路旁是安息公墓办事处。当我们开口询问林昭墓时，围上来一群老妇争着带路，每人手中都拿着扫帚和抹布。当然，她们的服务是要付费的。<br />
<br />
<img src="http://upload.17u.com/uploadfile/2007/04/03/6/2007040322451555003.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
图：右侧山路<br />
<br />
　　沿右侧山路前行几十米，右手旁是未经整修的韩世忠墓碑。<br />
<br />
<img src="http://upload.17u.com/uploadfile/2007/04/03/6/2007040322451670067.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
图：韩世忠墓碑<br />
<br />
　　从韩世忠墓碑上行几十米，山路两旁就是墓区了。在几乎到达山路尽头时，右侧路边可见“王志喜、仇阿风之墓”的墓碑。这排第二座就是林昭墓和林昭父母的合葬墓。<br />
<br />
<img src="http://upload.17u.com/uploadfile/2007/04/03/6/2007040322451685067.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
图：山路尽头<br />
