<br />
附2：<br />
　　　　　　　　　　上海市高级人民法院刑事判决书<br />
　　　　　　　　　　　（80）沪高刑复字第435号<br />
<br />
　　林昭，又名彭令昭、许萍，女，一九三二年生，江苏苏州市人，原北京大学学生。<br />
<br />
　　林昭于一九六五年五月上海市静安区人民法院以反革命罪判处有期徒刑二十年。一九六八年四月二十九日又由中国人民解放军上海市公检法军事管制委员会以反革命罪判处死刑，立即执行。现经本院覆查查明：林昭在一九五八年被错划为“右派分子”后因精神上受到刺激，一九五九年八月开始患精神病。嗣后，曾以写长诗、文章等表示不满，并非犯罪行为。一九六五年对林昭以反革命罪判处徒刑显属不当，应予纠正。林昭被错判服刑后，精神病复发，又曾用写血书、诗歌、日记以及呼喊口号等表示不服，一九六八年将林昭在病发期间的行为又以反革命处以极刑，显属错误，应予纠正，据此本院特重新判决如下：<br />
<br />
　　一、撤销上海市静安区人民法院一九六二年度静刑字第一七一号和中国人民解放军上海市公检法军事管制委员会一九六七年度沪中刑（一）字第一六号两次判决。<br />
<br />
　　二、对林昭宣告无罪。<br />
<br />
上海市高级人民法院<br />
一九八○年八月二十二日<br />
<br />
　　另，网上有称：1981年12月30日，上海高级人民法院复判林昭案件，宣布林昭无罪，为林昭平反。但未能查到判决书原文。不知是辗转记述有误，还是另有更彻底的平反。在此希望知道详情的朋友指教，最好有1981年12月30日上海高级人民法院复判的判决书提供。<br />
<br />
<br />
北国游子<br />
二○○七年四月三日<br />
<br />
三年过去，不知有无什么变化。知道的是：第二年，林昭墓地已经被装上监控摄像头。</div>

							
							
							
							
