<br />
图：路旁王志喜墓<br />
<br />
<img src="http://upload.17u.com/uploadfile/2007/04/03/6/2007040322451698695.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
图：林昭墓<br />
<br />
　　林昭墓前，摆放着一个花篮，和一迭打印的祭文，落款是：<br />
<br />
清泉水作<br />
满洲战神致<br />
2006年清明<br />
<br />
　　并盖了个篆章。我们开始也是走了冤枉路，耽搁了时间，未能与前来的二位谋面。为此，我尽量在这里多盘桓一会，期盼能遇见后到的朋友。问身边的带路老妇，说是无论清明中秋，还是什么日子，这里时常有人来祭奠。<br />
<br />
<img src="http://upload.17u.com/uploadfile/2007/04/03/6/2007040322451652068.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
图：致林昭姐姐1<br />
<br />
　　下面我抄录下这篇祭文，未经作者和祭献者的同意，但我想，他们应该是希望有更多的人看到的。<br />
<br />
致林昭姐姐<br />
<br />
姐姐，我看见你在永生上帝面前独自的落泪，但你仍跪拜着献上你每一天的虔诚。<br />
姐姐，我看见你眼中经过常人生活时的幽幽哀怨，但你仍对着山墙苦守每一天的寂寞。<br />
因为，<br />
我听见你的梦魂深处那不死的憧憬在不灭地低唱，<br />
听见永生上帝的爱在你血管离不死地跳跃。<br />
辗转反侧，心绪难安的夜里。<br />
天国的大门因你而洞开并摇曳，迸射出人世不曾有过的缤纷来宽慰你一颗伤痕累累的心；真理之神的声音划破天空，超越凡人的感官的所及，最终从你门前的金泉中下注，敲击出人世不曾有过的音乐，伴你入眠。<br />
因此<br />
年月中，你遇寒遇暖也都无所谓了。<br />
<br />
姐姐，你的眼中闪烁着睿智的光芒。如果你愿意，你随时可以成为一个到处激起男子保护欲的灰姑娘，一个受人景仰的仙女，一个万众迷恋的公主。<br />
姐姐，你的伪装无法遮掩你明艳照人的美态，如果你愿意，你随时可以挑选一个你所钟爱的痴心情郎，到世间情人的生命中穿梭一回。<br />
但你选择了扫帚，而不是镶满钻石的仙杖；你选择了缄默的爱，而不是以爱的名义与外表去攫取；你选择阴暗潮湿的山洞，而不是据说非常美好的宫殿。<br />
姐姐，我真的不懂，这些人值得你这样吗？<br />
姐姐，我真的不懂，难道你不知道家人的处境吗？<br />
但你告诉我，名誉不是必须的，而真心是必须的；成功不是必须的，而努力却是必须的；得到不是必须的，而付出却是必须的；生命不是必须的，而使命却是必须的――若心中无爱，则于死何异？若生已无欢，则死亦惧？<br />
姐姐，我必然要回去把你的资讯带给我们中的每一个人。<br />
<br />
我就这样<br />
久久地注视你的眼睛<br />
轻轻地取出你嘴里的棉团<br />
你的嘴唇依然柔软<br />
你的坟墓空空荡荡<br />
你的血烫伤了我伸出的手<br />
如此寒冷又残酷的死亡<br />
让独坐的我<br />
无法悲伤<br />
任何形式的墓地<br />
于热爱自由的你<br />
都过于轻浮<br />
<br />
每年的阴历十五<br />
河上会布满河灯<br />
却招不回你的灵魂<br />
你冷眼坐在<br />
卡夫卡笔下四处漂流的冥船上<br />
看这个世界依然荒唐<br />
北大校庆的举杯欢呼<br />
让你冷冷大笑<br />
喝吧喝吧喝吧<br />
这是血呢<br />
你在黑暗中说<br />
<br />
　　带路的老妇迅速打扫了墓地，我们献上准备好的鲜花。<br />
<br />
<img src="http://upload.17u.com/uploadfile/2007/04/03/6/2007040322461012492.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　打开带来的“银剑南”，斟满。我与英灵共饮。<br />
<br />
<img src="http://upload.17u.com/uploadfile/2007/04/03/6/2007040322461122651.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　点上烟，我不由得想：林昭在此安息，英灵已是无罪之身，但，仍背负着不实之名。<br />
<br />
<img src="http://upload.17u.com/uploadfile/2007/04/03/6/2007040322461161849.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　1980年8月22日，上海市高级人民法院所发《刑事判决书》中称：林昭是精神受刺激患精神病，故反革命罪判处徒刑显属不当，以反革命处以极刑显属错误，应予纠正。据此撤销判决，“宣告无罪”。（详见附后判决书）<br />
<br />
　　言下之意，林昭的表达与作为若是正常人的自主意识，还是罪属所当，不会纠正，更不会“宣告无罪”。当然，与时俱进了，不会再判反革命罪，可能会是煽动罪？或颠覆罪、危害国家安全罪？那么，我等对林昭的纪念和对漫漫兮修远的期盼，恐怕也是罪有所连。<br />
<br />
　　同时，目下在Google上搜索关键词“林昭”，网页上仍赫然可见：据当地法律法规和政策，部分搜索结果未予显示。<br />
<br />
　　路，漫漫兮，其修远兮……<br />
<br />
<br />
附1：林昭墓地的Google Earth截图。因Google Earth提供的卫星图片均为若干年前资料，所以图片上的灵岩山景点和韩世忠墓园似乎都还没有修建完工，这算是中国迅猛发展走在世界之前的一个事例吧。仅供参考。<br />
<br />
