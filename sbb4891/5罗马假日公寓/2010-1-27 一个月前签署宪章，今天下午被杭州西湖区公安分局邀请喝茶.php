													<h2>具结悔过</h2>
						
						
																											<div id="postmessage_454634" class="t_msgfont">具结悔过是刑法中的非刑罚处理方法之一，即责令免于刑事处罚的犯罪分子用书面方式，保证悔改。<br />
<br />
　　非刑罚处理方法是指对于犯罪分子判处刑罚以外的其他方法，主要包括以下三类： <br />
<br />
　　1．判处赔偿经济损失或责令赔偿损失。 <br />
　　这两种处罚是判令犯罪人赔偿被害人的经济损失，是刑事附带民事的强制处分，前者适用于被判处刑罚的犯罪分子，后者适用于犯罪情节轻微，不需要判处刑罚而免于刑事处罚的犯罪分子。 <br />
<br />
　　2．训诫，责令具结悔过及赔礼道歉 <br />
　　这一类处理方法适用于免于刑事处罚的犯罪分子，训诫即对其当庭公开谴责；责令具结悔过即责令其用书面方式，保证悔改；责令赔礼道歉是责令其承认错误，向被害人表示歉意。 <br />
<br />
　　3．由主管部门给予行政处罚或者行政处分。 <br />
　　人民法院根据案件具体情况，可向被告人所在单位提出行政处分的建议，由主管部门直接给予被告人以一定的行政处罚或者行政处分。<br />
