<br />
不同的心理学流派的咨询师在心理咨询中处于不同角色，但随着心理学发展，心理咨询（不是心理治疗，简单理解，心理咨询来访者属于程度较轻状态，场所是心理咨询室。而心理治疗来访者处于程度比较严重状态，场所为医院）师大多接受并认可了以来访者为中心的理念。<br />
<br />
可以这样理解，心理咨询师在心理咨询过程中，永远只处于分析状态，帮助来访者理清纷乱的思路，以便由来访者自己作出正确的选择和判断。而不是告诉来访者要怎么做。<br />
<br />
这一切均建立在这样一种理论上：只有当事人自己，才真正有对自己行为和思维的决定权。<br />
<br />
有以上可以看到，一位咨询师的经验是多么的重要，这种经验不只是职业素养，更是人生阅历。但这并不意味着咨询师越老越好。这些话有些矛盾，大家可以只当作咨询师自我要求来看。<br />
<br />
--------------------------------<br />
<br />
处于健康考虑，我建议大家不要过于“自信”。不论是找可靠的朋友倾诉，还是拨打免费心理咨询电话，还是直接邀约看心理医生，都比抗着强。<br />
<br />
-----------------------------<br />
<br />
最后，我简单描述一些心理咨询中涉及的道德伦理，以便大家对心理咨询师的素质进行一个判断。关于咨询师的技能素养，大家可以查看咨询师资质；机构背景等来综合判断。<br />
<br />
1、咨询师严禁与来访者产生任何感情上的纠葛，哪怕是来访者主动。这个禁止包括咨询前，咨询中，及咨询后<br />
这点告诉我们，不要害怕咨询师以咨询质量等为要挟要求发生感情，不要觉得是自己无法自拔的主动爱上对方，因为你处在心理弱势。<br />
<br />
2、咨询师严禁与来访者发生性关系或是任何的亲密接触（语言、行为）。这个禁止包括咨询前，咨询中，及咨询后<br />
理由如上<br />
<br />
3、以上禁止接触范围还应扩展到来访者的亲友<br />
<br />
4、咨询师严禁从来访者身上谋取除咨询费外的其他利益<br />
这点告诉我们，你与咨询师只需保持一种关系，即咨询时的契约关系。<br />
<br />
5、除非在发生自杀等危及来访者及他人生命等极端情况下，咨询师必须严格遵守保密原则，不得透露任何有关来访者信息，也不得在未经来访者同意情况下进行录音/录像。<br />
这点告诉我们，选择一个可靠的机构是保护我们权益的目的之一；咨询前你可以强调自己的隐私权；如果受到伤害，及时维护自身权益<br />
<br />
6、咨询师在发现自己能力不足时，会向来访者主动说明并推荐合适咨询师<br />
这点告诉我们，心理咨询也细分很多领域，比如家庭咨询/婚姻咨询/抑郁咨询等，不同咨询师所擅长不同，必须专业对口。另外大家不要太担心咨询师会为了赚钱黑心就上，在可靠的机构内，制约咨询师的机制还是有不少的。比如机构制度制约，咨询师相互制约，咨询效果制约等<br />
<br />
以上的这些，都是我这个不太专业的家伙根据自己了解而总结的东西，国内咨询师们有长篇的严格的职业道德规范。<br />
<br />
因为这不仅关系到来访者的利益，更关系到全体咨询师从业人员的利益。故不排除个别无良者的情况下，大家可以对咨询师保持信任。<br />
