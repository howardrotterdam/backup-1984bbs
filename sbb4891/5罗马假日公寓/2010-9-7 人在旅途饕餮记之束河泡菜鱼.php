&nbsp; &nbsp;后来就是潘驴邓小闲西门庆出现了，非常符合她的想象，走到一起顺理成章。<br />
&nbsp; &nbsp;如果在新社会，潘金莲肯定会义无反顾的选择做地主员外的填房。做有钱人的二奶好，她也就不会有后来的坎坷和悲剧结局了。<br />
