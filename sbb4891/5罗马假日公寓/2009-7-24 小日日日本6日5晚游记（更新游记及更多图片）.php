													<h2>浅尝辄止话日本（贰：初来乍到）</h2>
						
						
																											<div id="postmessage_191894" class="t_msgfont">如果香港算不上出境，那日本之行就是我的出境处女游了。坐上前往宾馆的巴士，我的眼睛贪婪地吮吸着车窗外每一处的风景。一尘不染的马路，矮矮小小的房屋，就连警车，也是迷你的size。以前在日剧和电影里看过无数次的街景，如今鲜活地跃入视线，仿佛置身于巨大的日剧拍摄基地，或是多拉A梦和樱桃小丸子所住的街区，真是个童话般的国家。<br />
<br />
&nbsp; &nbsp; 第一晚入住的是位于成田k、今年新开不久的APA Hotel，各种设施都是簇新的。我们被安排在了单人间，用俗话说，就是“麻雀虽小五脏俱全”。在房间里基本上只剩下转身的空间，但和国内的商务酒店比，多了不少人性化的细节。床上叠着一套浴衣，上面放了一只折好的千纸鹤。卫生间是一个整体，包括水处理系统、马桶（旅馆和机场的马桶基本都有热水洗屁屁功能）、面盆和小小的浴缸，都是连在一起的。水龙头里流出的水，可以直接喝。浴室里备有资生堂的洗发、护发素和沐浴露。<br />
<br />
&nbsp; &nbsp; 在日本的我，就是一个硬盘啦。一踏进楼下的Lawson便利店，我便眼花缭乱地high了。上海的“全家Family Mart”固然已算不错，但是和日本的Lawson一比，绝对完败。品种繁复的零食、便当、点心、饮料、日用品、化妆品、甚至还有18禁杂志（内页用胶纸封住，小盆友是翻不开的哦）……大概价格要比久光百货B1超市便宜些。<br />
<br />
&nbsp; &nbsp; 整洁的街道、繁琐的垃圾分类、严格遵循右行左立的电梯、公共场所的轻声细语、礼貌的待人接物、秩序井然的排队……当我身处于这种更高的社会素质之中时，总会有心存敬畏之感。在香港如此，在日本亦然。并不是说外国的月亮就特别圆，而是那种无所不在的自律和文明举止，会让人自然而然地有所触动，进而与自己的城市作比较。我沮丧地认为，要在上海实现这种真正让人感到和谐、井然的社会氛围，可能还有很长很长的路要走。<br />
<br />
&nbsp; &nbsp; 自动贩售机是日本街头“一道亮丽的风景线”，（原谅我用了如此恶俗的词组 :P ）。往往四五个自动贩售机连成一排，每台都提供超多选择、品种各异的冰冻饮料、新鲜咖啡等等。价格在100-200易笥遥和便利店的价格没差。BTW，日本的星巴克，一杯咖啡约在300易笥遥一个京都限定马克杯价格1000页鐾罚和国内差不多的，ToT 。<br />
<br />
&nbsp; &nbsp; 在日本的几个城市之间穿行，一共住了5晚。自助式早餐品类多，既新鲜，也很可口。一般都会分成日式和西式的两种。日式提供米饭、味噌汤、厚蛋烧和各式佐餐小菜，更好一点的，还有可爱的饭团和纳豆；在最后一天的早晨，吃到了新鲜出炉的羊角面包，拿在手上还有点烫烫的，一口咬下去，那种感动，完全像漫画《日式面包王》里那些夸张的表情，鲜花盛开，天使盘旋，世界真美好~值得一提的是日本的鸡蛋、牛奶和豆浆，可能是饲料的不同，味道真的和国内差很多，牛奶更香浓，豆浆比我妈用九阳豆浆机做出的还好喝很多，啊啊啊，真想天天吃到呢~<br />
<br />
&nbsp; &nbsp; 每到一处，都会去当地的厕所“参观留念”。在日本如厕，虽说不上是什么享受，但绝无异味、脏污（包括公园和停车场的厕所）。而且座便器的分类很细，给婴儿换尿布的、小孩用的、坐式、蹲式、残障人士专用的……厕所里基本都会备上2筒纸巾，一次性坐垫，放手包的小隔板等便利措施。厕纸和国内不一样，遇到水会化掉，所以用完的纸张是要求扔进马桶抽掉的，女生的卫生用品也被要求放入专门袋子里后再投入垃圾桶。让人无奈的是，很多景点和停车场的厕所都贴上了用简体中文写的“如何用厕”的图示标语，第一条：“请坐在马桶圈上用厕”，实在令人汗颜不已。<br />
